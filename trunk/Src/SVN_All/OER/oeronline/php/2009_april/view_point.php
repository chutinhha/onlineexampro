		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    <img border="0" src="view.jpg" align="right" hspace="8" vspace="6" width="100" height="119"></font><font face="Verdana" size="4"><b>Doing business in 
                Singapore<br>
                </b></font><font face="Verdana" size="1"><b>Strategic location, 
                excellent connectivity, world-class airport and seaport 
                facilities, strong business support, a skilled workforce and a 
                stable political environment are major draws for global 
                businesses to site their headquarters in Singapore<br>
&nbsp;</b></font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>
                <font color="#CC3300">By Sridhar Sridharan</font></b></font><br>
                <br>
                <font face="Verdana" size="1"><font color="#000080"><b>Business environment</b></font><br>
                Singapore is a city-state island country located at the southern 
                tip of the Malaysian Peninsula in South East Asia. <br>
                <br>
                Singapore is a major financial centre in the region and a 
                leading provider of services such as international banking, 
                trade finance, maritime finance, insurance, treasury operations, 
                and asset and wealth management within the region.<br>
                <br>
                Strategic location, excellent connectivity, world-class airport 
                and seaport facilities, strong business infrastructure support, 
                a skilled workforce and a stable political envi ronment are 
                major draws for global businesses to site their headquarters in 
                Singapore and use Singapore as a launch pad for tapping regional 
                and global business opportunities. The World Bank also ranks 
                Singapore as the world’s easiest place to do business.<br>
                <br>
                Businesses with global operations can benefit from Singapore’s 
                wide network of Double Taxation Agreements (DTA), Free Trade 
                Agreements (FTA) and Investment Guarantee Agreements. Singapore 
                attaches great importance to the protection of Intellectual 
                Property Rights (IPR). Singapore’s workforce is generally highly 
                educated, skilled and competitive. The prevalent business 
                language is English.<br>
                <br>
                <b><font color="#000080">Economic overview</font><br>
                </b>The inflation rate averaged 6.5 per cent in 2008. The prime 
                interest rate as at 31 December 2008 was 5.38 per cent.<br>
                <br>
                Singapore is committed to maintaining international 
                competitiveness and has adopted the free market system so that 
                the industries can respond quickly to market signals and adjust 
                their strategies accordingly.<br>
                <br>
                The Singapore economy is open to the world, in trade and 
                investment. In 2007, Singapore trade to GDP ratio was 348 per 
                cent, the highest in the world. The Global Competitiveness 
                Report 2008-2009 by the World Economic Forum ranks Singapore 
                fifth in its Global Competitiveness Index.<br>
                <br>
                Singapore supports free trade. This includes proposals aimed at 
                fostering trade among nations like the Asia-Pacific Economic 
                Co-operation and the ASEAN Free Trade Area. To this end, 
                Singapore has a network of 13 FTAs which are designed to 
                position Singapore as an integrated manufacturing hub in this 
                region, promote research and development in Singapore’s 
                knowledge-based economy and drive the services hub. With FTAs, 
                Singapore-based exporters and investors stand to enjoy a myriad 
                of benefits like tariff concessions, preferential access to 
                certain sectors, faster entry into markets and IPR protection.<br>
                <br>
                Many government development agencies, such as International 
                Enterprise Singapore and Economic Development Board provide 
                advice and assistance to further economic development.<br>
                <br>
                <font color="#000080"><b>Incentives</b></font><br>
                Singapore welcomes foreign investment and virtually all business 
                sectors are open to investors. Income tax rates have been 
                progressively reduced and are now very competitive. Generally, 
                all businesses are able to apply for various types of incentives 
                in the form of grants, tax incentives, etc. subject to meeting 
                certain qualifying conditions. Specific incentives of a varying 
                nature are also available to encourage the development of 
                certain industries.<br>
                <br>
                <font color="#000080"><b>Type of business entities</b></font><br>
                A number of types of business entities are possible. All 
                entities must be registered with the Accounting and Corporate 
                Regulatory Authority (ACRA). In summary, the different types of 
                key business entities and their key differentiating features are 
                as follows:<br>
                <br>
                <i><b>Companies</b><br>
                </i>Companies are generally limited by shares and a company can 
                qualify as a private or exempt private company as long as the 
                number of its shareholders is less than 50 or 20 respectively. 
                An exempt private company cannot have a corporation as a 
                shareholder. These companies are subject to less stringent rules 
                than public companies.<br>
                <br>
                <i><b>Sole proprietorship and partnerships</b><br>
                </i>Sole proprietorship and partnership are generally subject to 
                lesser statutory requirements than companies, but the partners 
                and the proprietors do not have the protection of limited 
                liability. A partnership may not have more than 20 partners.<br>
                <br>
                <i><b>Limited liability partnerships (LLP)</b><br>
                </i>An LLP gives owners the flexibility of operating as a 
                partnership while having a separate legal identity like a 
                private limited company. The LLP has perpetual succession, which 
                means any change in the partners of a LLP will not affect its 
                existence, rights or liabilities.<br>
                <br>
                <i><b>Branches of foreign companies</b><br>
                </i>A foreign company that wishes to establish a place of business 
                in Singapore can choose to register a branch in Singapore. The 
                foreign company will assume the liability of the branch.<br>
                <br>
                The company must appoint two local agents in Singapore to act on 
                behalf of the company. The agents must be ordinarily resident in 
                Singapore.<br>
                <br>
                <b><font color="#000080">Business entity used by foreign investors</font><br>
                </b>The form of business entity which is set up in Singapore by 
                foreign investors can be different and is often driven by their 
                different commercial operations and needs. Nevertheless, a 
                company structure is commonly used for most types of businesses 
                by foreign investors.<br>
                <br>
                A company is formed by filing its memorandum of association and 
                articles of association, together with certain specified forms, 
                with the ACRA. Incorporation of a company can be immediate upon 
                the e-filing of the incorporation documents, only subject to the 
                approval of the company name which can take up to 14 working 
                days. No minimum capital requirements are imposed.<br>
                <br>
                After the business entity is established, it needs to consider 
                its Goods and Services Tax (GST) registration and other 
                applicable licensing requirements. Special licenses are needed 
                for some businesses such as banking, insurance and stock 
                broking.<br>
                <br>
                <font color="#000080"><b>General applicable tax rules </b> <br>
                </font>The Singapore taxation system operates on a territorial basis, 
                i.e. income sourced in Singapore is subject to tax. Capital 
                gains are generally not taxed in Singapore. In addition, foreign 
                income derived is taxable only upon remittance into Singapore.<br>
                <br>
                Both resident and non-resident companies (including branches) 
                are subject to tax at the same standard corporate tax rate 
                currently at 18 per cent* with a partial tax exemption on the 
                first S$300,000 normal chargeable income as follows:</font><ul>
                  <li><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">75 per cent exemption of up to the first S$10,000; and 50 per 
                cent exemption of up to the next S$290,000.</font></li>
                  <li><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">There is no further tax on dividends.</font></li>
                </ul>
                <p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">Resident companies are also granted tax exemption on certain 
                foreign-sourced income subject to meeting certain conditions. 
                Where a foreign company has a presence in Singapore, the 
                taxation of its income will depend on the nature of the income 
                derived and whether a DTA exists between Singapore and the 
                country of residence of that company. <br>
                <br>
                <b><font color="#000080">Withholding taxes (WHT)</font><br>
                </b>There is no WHT on dividends or on the remittance of after tax 
                branch profits to a foreign company. In general, payments for 
                interest, rental of moveable properties, royalty, technical 
                services, director fees and management services to non-residents 
                may be subject to Singapore WHT. Depending on the nature of the 
                payment, the WHT rates lie between 10 per cent to 20 per cent. 
                Certain payments may be exempt from WHT under administrative 
                concessions if specific conditions are met. Applicable DTA may 
                also exempt or reduce the withholding rate.<br>
                <br>
                <font color="#000080"><b>Individual income tax</b><br>
                </font>Individual income tax is levied at progressive rates, with a 
                maximum marginal rate of 20 per cent. The employment income of 
                non-resident individuals is taxed at 15 per cent or at the 
                resident rate, whichever gives rise to higher tax. Generally, 
                foreign-sourced income, received by both resident and 
                non-resident individuals are exempt from Singapore income tax. 
                Remittances of foreign sourced income through a partnership are 
                generally taxable, unless under specific circumstances.<br>
                <br>
                <font color="#000080"><b>Goods and services tax (GST)</b><br>
                </font>GST is levied on the supply of goods and services in Singapore, 
                and on the importation of goods into Singapore. It is a 
                value-added tax system. The current prevailing rate is 7 per 
                cent.<br>
                <br>
                <font color="#000080"><b>Other taxes</b></font><br>
                Other significant taxes to be considered by the foreign 
                investors include property tax, foreign workers levy, skills 
                development levy, stamp duties and customs and excise duties.<br>
                <br>
                <font color="#000080"><b>Double taxation agreement (DTA)</b><br>
                </font>Singapore has inked over 60 comprehensive DTAs and limited 
                treaties with other countries. It has a DTA with the Sultanate 
                of Oman that came into force in 2007. Under Singapore rules, a 
                foreign tax credit is limited to the lower of the foreign tax 
                paid and the Singapore tax payable on that income. The credit is 
                generally granted on a country-by-country, source-by-source 
                basis. A unilateral tax credit system, similar to treaty relief, 
                is also available for all foreign-sourced income remitted to 
                Singapore from non-DTA countries.<br>
                <br>
                <font color="#000080"><b>Labour availability</b><br>
                </font>Singapore’s highly educated and skilled workforce is geared to 
                support its knowledge driven and capital intensive economy. 
                Singapore also has an open immigration policy which gives 
                companies the opportunity to source for the best personnel from 
                anywhere in the world.<br>
                <br>
                <font color="#000080"><b>Social security</b><br>
                </font>Singapore does not have a general social security system. 
                However, Singapore has a statutory savings scheme called The 
                Central Provident Fund (CPF). Both employers and employees who 
                are Singapore citizens and permanent residents working in 
                Singapore are required to contribute to the CPF. Foreigners are 
                exempt from CPF contributions.<br>
                <br>
                <font color="#000080"><b>Foreign exchange controls </b> <br>
                </font>Singapore does not impose any restrictions on the remittance or 
                repatriation of funds in or out of Singapore.<br>
                <br>
                <font color="#000080"><b>Import regulations</b><br>
                </font>In general, non-controlled goods may be imported into Singapore 
                without the need for licences. The import of certain goods into 
                Singapore is subject to control by the relevant authorities 
                while the import of certain other goods is strictly prohibited. 
                Traders of such controlled goods must have the necessary 
                licences before they can import the goods.<br>
                <br>
                <font color="#000080"><b>Summary</b><br>
                </font>A strategic base to the emerging Asian markets, Singapore is a 
                premier starting point for companies seeking business 
                development in the region. With excellent infrastructure, a 
                skilled workforce and an extensive trade network with other 
                economies, Singapore can effectively connect any investor to the 
                world, opening new business opportunities for the investor.<br>
