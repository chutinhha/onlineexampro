                reports of Dubai Government’s sovereign bond issuance of $20bn. 
                The injection of much needed liquidity into Dubai coupled with 
                the support from the federal government was cheered by the 
                investor community. Kuwait markets ended down despite strong 
                action from the local government to support banks and rescue the 
                economy. Kuwait’s Central Bank guaranteed 50 per cent of new 
                loans extended by local banks to investment firms in 2009 and 
                2010. This was in addition to the rescue bill by the Kuwait 
