                <b>A pull back in international commodity prices and higher 
                dividend payout by industrial companies helped bring some cheer 
                to an otherwise dull market</b><br>
                <br>
                MSM30 benchmark index continued to slide down led mainly by Bank 
                Muscat and Omantel stocks. The Industrial sector index 
                outperformed with the industrial index treading up 20 per cent 
                for the period from 16th February 2009 to 15th March 2009. <br>
                <br>
                Some pull back seen in the commodity prices owing to demand 
                increase across the world and higher dividend payout by 
                industrial companies increased investor interest in industrial 
                sector stocks like Oman Cables (+95.23 per cent), Jazeera Steel 
                Products (+20.2 per cent) and Raysut Cement (25.9 per cent) 
                treading up with significant volumes. Services and Banking 
                sector index continued to tread down mainly due sell off seen in 
                heavy weights; BankMuscat and Omantel. Broader market however 
                didn’t indicate to any major sell off with advance decline ratio 
                of 1:1. The month of February witnessed large investors and 
                institutions churning their investment portfolios which could be 
                identified as a single major reason for heavy selling seen in 
                some of the major counters and buying in some. <br>
                <br>
                <b>Corporate events<br>
                </b>In key corporate events, Galfar Engineering won a contract 
                worth RO19.348mn for the construction of New Jetty at SBSNB, 
                Wudam for the Ministry of Defence. The period from 15th February 
                to 15th March 2009 saw almost all listed companies reporting 
                results and dividend for the year 2008. Omantel and Raysut 
                Cement proposed to pay 100 per cent dividend for the year 2008.
