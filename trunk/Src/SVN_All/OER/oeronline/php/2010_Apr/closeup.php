                    <font face="Verdana" size="1"><br>
					</font><b> 
                    <font face="Verdana" size="4" color="#CC3333">Saudi Arabia’s 
					credit rating upgraded<br>
					</font>
			</b><font face="Verdana" size="2"><b><br>
					</b></font><b><font face="Verdana" size="2">As the world’s 
					largest exporter of crude oil, Saudi Arabia’s economic 
					growth and inflow of FDI have boosted its ratings and 
					ranking in the global arena</font></b><font face="Verdana" size="1"><br>
					<br>
					<b>Recently Moody’s International Services made the right 
					decision in upgrading credit rating of Saudi Arabia. It 
					raised ratings for foreign and local currency debt plus 
					ceiling for foreign currency bank deposits one notch from A1 
					to Aa3, in turn considered the fourth highest grade.</b><br>
					<br>
					<b>Four attributes<br>
					</b>A report appearing at the Wall Street Journal attributed 
					Moody’s move to four major points, namely strong budgetary 
					position, a resilient banking system, current account 
					surplus and substantial foreign exchange reserves. <br>
					<br>
					To be sure, Saudi Arabia’s 2010 budget points out to steady 
					increase in public expenditures. With projected spending of 
					$144bn, the kingdom’s 2010 budget reflects continued 
					determination to meet challenges posed by the financial 
					crisis. The figure shows growth of 14 per cent versus 
					original spending level for fiscal year 2009.<br>
					<br>
					On a negative point, this spending level causes a sizable 
					deficit of $19bn, representing nearly five per cent of gross 
					domestic product (GDP) at current prices. The shortfall 
					violates one criteria of Gulf Monetary Union (GMU) project. 
					The GMU restricts budget deficit to three per cent of the 
					GDP. Saudi Arabia, Kuwait, Qatar and Bahrain started 
					implementation of GMU from 2010.<br>
					<br>
					Though not released, it is assumed that Saudi authorities 
					developed the 2010 budget using conservative average oil 
					price. Assumed oil price is uniquely vital by virtue of the 
					petroleum sector contributing nearly three quarters of 
					treasury income. In fiscal year 2009, the kingdom posted 
					actual deficit of $12bn, down from $17bn projected figure 
					despite increasing actual expenditures by $21bn to $147bn. 
					At any rate, chances are that the 2010 shortfall would 
					largely be contained due to stronger oil income.<br>
					<br>
					<b>Banking prospects<br>
					</b>As to banking variable, a good number of Saudi banks 
					weathered the worst effects of global financial crisis, with 
					National Commercial Bank, the largest Saudi bank by assets, 
					seeing its net profit doubling to $1.08bn in 2009.<br>
					<br>
					Still, there are promising prospects, with Saudi authorities 
					showing determination to turn Riyadh into a major centre for 
					financial services on a global level through King Abdullah 
					Financial District (KAFD). Construction of KAFD started in 
					2007 with completion of the first phase expected by year-end 
					2010. Amongst others, KAFD would house numerous entities 
					dealing with financial matters including the bourse.<br>
					<br>
					<b>International business<br>
					</b>At the same time, Saudi Arabia enjoys surpluses in its 
					international dealings, reporting current account and trade 
					balance surpluses of $203mn and $1033mn respectively in 
					2009. Undoubtedly, oil prices play a key role in actual 
					current account and trade balance results, as the petroleum 
					sector accounts for nearly 90 per cent of export earnings 
					and 40 per cent of gross domestic product (GDP). According 
					to BP Statistical Review of World Energy, Saudi Arabia is 
					the world’s largest exporter of crude oil, on the back of 
					sustained output of nearly 10 million barrels per day.<br>
					<br>
					In addition, Saudi Arabia boasts some $410bn in foreign 
					exchange reserves. This is a sizable figure by virtue of 
					exceeding the country’s GDP of $385bn last year. In reality, 
					Saudi authorities reported 22 per cent drop in nominal GDP 
					in 2009 due to adverse developments in the petroleum sector 
					reflecting lower average oil prices in 2009 versus 2008. 
					Certainly, international agencies and investors appreciate 
					this kind of transparency.<br>
					<br>
					<b>Growing recognition<br>
					</b>In addition, other international reports point out to 
					outstanding economic results in Saudi
					</font><font face="Verdana" size="1">Arabia, even more 
					confirming steady economic developments in the Kingdom. For 
					instance, according to World Investment Report 2009, issued 
					by World Conference on Trade and Development (UNCTAD), 
					inward foreign direct investments (FDI) into Saudi Arabia 
					amounted to $38.2bn in 2008. This translates into a growth 
					of $13.8bn or 36 per cent in a single year. The figure is 
					substantial by virtue of representing some 42 per cent of 
					total FDI amounts of West Asian countries, which include GCC 
					states plus Turkey and Iran. The other top regional 
					recipients, namely Turkey and the UAE attracted FDI worth 
					$18.2bn and $13.7bn, respectively.<br>
					<br>
					To be sure, the size of FDI has increased substantially over 
					the last few years, amounting to $24.3bn in 2007, $18.3bn in 
					2006 and $12bn in 2005. Conversely, inbound FDI averaged 
					merely $245mn <br>
					for the period spanning 1990 to 2000.<br>
					<br>
					The extraordinary progress partly reflects improvements of 
					the country’s foreign investment law (FIL). Amongst others, 
					FIL grants foreign firms the right to own majority stakes in 
					companies within the Kingdom. In addition, the law allows 
					foreign banks to operate in the form of locally incorporated 
					joint stock companies or as branches of international 
					financial institutions. Also, Saudi Arabian General 
					Investment Authority (Sagia), which looks after foreign 
					investments, has put in place a one-stop-shop application 
					process besides a 30-day deadline for decisions on 
					investment applications.<br>
					<br>
					</font>
                <b> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <img src="images/closeup01.jpg" hspace="9" vspace="5" border="0" align="right" width="320" height="258" style="border: 3px solid #EFEFE9; "></font></b><font face="Verdana" size="1"><b>Business 
					friendly<br>
					</b>Such practices are partly credited for Saudi Arabia’s 
					outstanding result achieved in World Bank’s ‘Doing Business 
					2010’ report. The report ranks Saudi Arabia at number 13 
					amongst 183 economies, the best within the entire Mena 
					region. In fact, the report ranks Saudi Arabia as the best 
					country in the world on the registration of property 
					variable. However, the report notes that Saudi Arabia has 
					difficulty with enforcing contracts, ranked as 140 globally.
					<br>
					<br>
					Also, Saudi Arabia has made notable progress in addressing 
					transparency problems. The Corruption Perceptions Index or 
					CPI, in turn issued by Transparency International ranked 
					Saudi Arabia number 63 globally amongst 180 nations in its 
					2009 report. This translates into Saudi Arabia advancing by 
					some 17 positions, the best progress achieved by any GCC 
					state.<br>
					<br>
					Rewards and surpluses aside, Saudi authorities cannot 
					overlook some of the key challenges facing the Kingdom. The 
					idea is to avoid overlooking the employment problem amongst 
					locals not least due to critical demographics. Some 38 per 
					cent of Saudis are below the age of 14 and thus many youth 
					would enter the job market in the years to come looking for 
					suitable employment opportunities. <br>
					<br>
					The challenge relates to ensuring availability of job 
					opportunities that meet the expectations of locals notably 
					females, with regards to working conditions and 
					remunerations.</font></p>                
