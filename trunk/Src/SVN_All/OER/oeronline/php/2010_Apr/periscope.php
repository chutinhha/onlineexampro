                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000">The 
				credit crisis and the future of Gulf banking</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
                </font><b><font face="Verdana" size="2">The Saad Group-Al 
				Gosaibi scandal in Saudi Arabia and the Dubai World debt crisis 
				will force the regional banks to revert and stick to the 
				fundamentals of sound banking</font><font face="Verdana" size="1"><br>
                <br>
                </font></b><font face="Verdana" size="1"><b>The world has 
				emerged from the worst banking crisis and economic recession 
				since the 1930’s but at a steep cost. The Federal Reserve’s zero 
				interest rates and epic monetary easing policies prevented 
				another Great Depression and engineered a successful rescue of 
				America’s largest banks. A similar HM Treasury bailout occurred 
				in Britain after the spectacular near flameout of RBS and Lloyds 
				HBOS. Global banks forced to dismember vast offshore empires, 
				handcuffed by taxpayer-financed bailouts and governments 
				ownership stakes, forced by the capital markets and their own 
				risk-averse boardrooms to de-leverage their balance sheets, will 
				no longer expand their business in the Gulf. Even international 
				banks with a historic presence in the Gulf such as Citigroup, 
				ABN Amro, RBS and UBS will shrink their presence in major Gulf 
				banking markets.<br>
				</b><br>
				<b>Down but not out<br>
				</b>The global economic scenario and local government policies 
				will shape the competitive environment of Gulf banking in the 
				next decade. While the global economy has emerged from 
				recession, the credit crunch is not over in the Gulf. Loan 
				growth is anemic, the asset securitisation model is broken and 
				offshore wholesale money markets have largely blackballed bank 
				borrowers with below investment grade credit ratings.<br>
				<br>
				International financial centres in the Gulf have witnessed the 
				exodus of several dozen banks and even banking markets such as 
				project finance, trade finance and interest rate swaps are 
				frozen by a new unwillingness to lend unsecured credit, even in 
				the inter-bank world. Yet the brave new world of Gulf banking is 
				not all doom and gloom. Government initiatives such as Saudi 
				Arabia’s $400bn infrastructure spending programme and six 
				economic cities will be hugely positive for bank lending. Crude 
				oil prices above $80 guarantee current account and budget 
				surpluses in Gulf oil exporter states. A new paradigm of risk 
				management and balance sheet liquidity suffuses Gulf banking.<br>
				<br>
				<b>Game changer<br>
				</b>The credit crisis of 2008- 09 will have a profound, 
				long-term game changer impact on the GCC banking system. For 
				one, the $20bn debt shock generated by the Saad Group-Al Gosaibi 
				scandal in Saudi Arabia will dampen “name lending” by GCC banks 
				to merchant family owned conglomerates. After all, the fact that 
				the Al Gosaibi Group was among the Saudi kingdom’s largest, most 
				respected corporate blue chips did not save its bank creditors 
				because they had no idea, let alone control, over its balance 
				sheet leverage or financial governance.<br>
				<br>
				Secondly, the era of breakneck 30-40 per cent annual credit 
				growth financed by offshore wholesale money markets at razor 
				thin credit spreads are going to go the way of the brontosaurus. 
				Dubai’s debt crisis, with international banks holding more than 
				$50bn in loans to local banks and state owned borrowers, means a 
				secular fall in money centre banks lending to the Gulf.<br>
				<br>
				Commercial bank loan book growth will be constrained by their 
				ability to generate organic deposits as the wholesale money 
				markets in London will no longer offer unsecured interbank 
				lending facilities. Bank borrowers from Dubai, for instance, are 
				no longer able to access the Eurobond medium term note (MTN) or 
				Euro-commercial paper market.<br>
				<br>
				<b>Repeating mistakes<br>
				</b>Thirdly, GCC banks, like French generals of the old school, 
				keep fighting the last war and do not heed the lessons of 
				history, so they keep repeating their mistakes. The UAE banking 
				system was gutted by a property crash in the 1980’s yet the 
				memories of a banking crisis that triggered a wave of private 
				bank failures and forced mergers did not prevent the next 
				generation of UAE bankers from playing Russian roulette in high 
				risk, speculative lending to off plan flippers and ridiculously 
				overleveraged developers.<br>
				<br>
				The Souk Al Manakh crash gutted the Kuwaiti banking system in 
				1982 yet almost a hundred Kuwaiti investment companies are 
				financial zombies a generation later, victims of failed 
				leveraged, stock market and property speculation.<br>
				<br>
				Fourthly, I expect GCC banks to reduce the scale of their 
				balance sheet leverage. Commercial banks with loan to deposit 
				ratios higher than 70-80 per cent will have to pay a risk 
				premium in interbank or global wholesale funding markets. Banks 
				owned by overleveraged governments will no longer be able to 
				camouflage reckless, speculative lending behind the veneer of 
				“state support”.<br>
				<br>
				Dubai World, where UK banks alone have a $5bn exposure in 
				syndicated bank debt, has exposed the folly of relying on 
				“implicit” government support. This is the ostensible reason 
				that the credit agencies downgraded the debt of dozens of Gulf 
				quasi sovereign corporate and bank borrowers after the shock 
				November 2009 Dubai World announcement of a standstill agreement 
				with its creditors.<br>
				<br>
				<b>Few but big<br>
				</b>Fifthly, I expect the credit crunch will lead to a trend 
				towards larger, fewer banks. It is no coincidence that regional 
				de facto national champions like Oman’s Bank Muscat, Dubai’s 
				Emirates NBD, Qatar’s QNB are all commercial banks whose 
				strategic DNA was forged by a succession of bank mergers. 
				Smaller, thinly traded or unlisted banks have no future in the 
				post-crisis banking constellation of the Gulf. The mania for new 
				bank IPO’s and phantom paper riches during the boom has created 
				dozens of financial intermediaries who will not survive the 
				competitive Darwinian shakeout in Gulf banking in the decades 
				ahead.<br>
				<br>
				Sixthly, the debt woes of Kuwait’s global and the UAE’s Shuaa 
				Capital means that the era of highly leveraged, hyper growth, 
				independent Gulf investment banks active in everything from 
				equity underwriting to Islamic finance to asset management is 
				now definitely over.<br>
				<br>
				Investment banking can no longer be the revenue growth El Dorado 
				of Gulf finance at a time when the asset securitisation model is 
				frozen, the IPO window on the stock exchanges is closed and 
				private equity deals can no longer be leveraged with high-risk 
				debt and derivatives due to fears of regulatory backlash. 
				Commercial banks will spawn investment banking divisions, as 
				Kuwait’s NBK, Oman’s Bank Muscat and the major Saudi banks have 
				done to serve corporate clients.<br>
				<br>
				Finally, Gulf bankers will learn that asset/liability mismatches 
				can prove fatal, as bank failures have proven for centuries. It 
				is a terrible idea for commercial banks to confuse their mission 
				with property developers or stock exchange croupiers. After all, 
				the public entrust them with their life savings and do not 
				deserve these periodic systemic crisis.<br>
