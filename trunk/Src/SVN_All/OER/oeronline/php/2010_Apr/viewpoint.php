                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    Lessons From Change – Banking and Capital Markets</font></b><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2"><br>
				In this article we look at lessons learned from the financial 
				crisis along with key areas that present opportunities and 
				challenges<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				<b>A new performance agenda is emerging as banks and financial 
				institu tions react to the economic downturn and prepare for the 
				recovery ahead. The response of banks to the crisis has been 
				varied, differing in scope and speed of execution. <br>
				</b><br>
				<b>Challenging situation<br>
				</b>The global financial crisis has had a profound impact on the 
				banking industry. It has affected banks of all sizes, forcing 
				institutions to grapple with reduced public confidence, 
				heightened shareholder scrutiny and increased regulatory 
				oversight. Throughout the crisis, troubled banks have been in 
				need of government assistance. The current market turmoil will 
				possibly yield a fair number of positive outcomes as banks 
				re-assess the way they do business.<br>
				<br>
				Chief among the lessons learned is that banks must improve the 
				way they manage risk across. Banks have also been reminded that 
				liquidity is crucial. Through this crisis, one thing has become 
				clear: the financial services industry is in a state of constant 
				flux. <br>
				<br>
				<b>Separate risk class<br>
				</b>The dramatic loss of liquidity experienced during the 
				financial crisis was a wake-up call for the banking industry and 
				exposed inherent weaknesses in the way that organisations manage 
				and control liquidity risk. While not exactly forgotten, it’s 
				fair to say that in the years leading up to the crisis many 
				financial firms viewed liquidity risk as a theoretical issue. 
				Liquidity risk must be treated as a separate risk class, 
				aggressively managed and elevated to a level equivalent to that 
				of credit, operational and market risks. <br>
				<br>
				<b>Liquidity management<br>
				</b>The crisis exposed the inadequacies and failures of 
				thoroughly quantifying potential liquidity risks. Going forward, 
				regulators expect banks to overhaul existing stress-testing 
				processes and abandon siloed risk-based testing frameworks. <br>
				<br>
				They recommend banks use market-wide and institution-specific 
				stress tests applied in both the short and long term. Several 
				regulators around the world have begun providing guidance on how 
				to strengthen liquidity risk management practices. The Basel 
				Committee on Banking Supervision as well as the UK’s Financial 
				Services Authority have issued several guidelines in this 
				regard.<br>
				<br>
				<b>Stress-testing <br>
				</b>Siloed infrastructures; outmoded systems; disjointed, 
				reactive reporting; and a dearth of interpretive, insightful 
				risk analysis emerged as key barriers to banks attaining a 
				holistic view of risk management. As institutions recover, 
				companies will be setting the vision and processes that will put 
				them on the path to the ultimate goal of achieving an 
				organisation-wide view of risk. Firms must ensure that risk 
				management is recognised across the organisation as a critical 
				component of decision-making, not as a compartmentalised, 
				analytical function.<br>
				<br>
				Firms did not recognise just how bad things could actually get. 
				Stress-testing, as an integral piece of risk forecasting, is 
				moving into the spotlight, as it provides banks with an idea of 
				their exposures in extreme circumstances.<br>
				<br>
				<b>cost-cutting<br>
				</b>As banks confronted the recent economic crisis, they were 
				jolted by declining revenue growth, toxic assets and weak 
				economic indicators. These brought about a strong focus on 
				cost-cutting and improvement, including, but not limited to: 
				streamlined support functions; short-term cost, cash and tax 
				management; as well as compensation and real estate planning.<br>
				<br>
				Another means of improving performance is by better aligning 
				risk and finance. Risk and finance are two sides of the same 
				value coin, and there’s no room for miscommunication. CFOs need 
				to set the stage for a better process by developing a single set 
				of critical indicators that both functions fully understand and 
				use to assess risk-based returns. There is a greater awareness 
				that financial institutions should link processes, information 
				and metrics across the operating model – from enterprise and 
				business unit strategic planning through operational execution 
				and performance assessment.<br>
				<br>
				<b>financial management<br>
				</b>A strategic approach to connect new financial information 
				systems with risk management is to develop an integrated, 
				single-source environment to promote efficiency, improve data 
				quality and take advantage of existing commonalities between 
				risk data and financial data. Standardising data definitions 
				across the risk and finance functions is a good first step.<br>
				Business optimisation<br>
				<br>
				As a result of the crisis, many institutions are focused on 
				streamlining their business operations. This can be a major 
				undertaking as banks are challenged by multiple locations, 
				businesses and legal entities. Institutions have been compelled 
				to identify their core competencies and focus on what they do 
				best.<br>
				<br>
				Conversely, there are several banks well positioned to expand 
				their business through strategic acquisitions. To seize these 
				opportunities, a fair amount of a0ssiduousness is required to 
				create a new entity capable of thriving in today’s 
				still-recovering global markets.<br>
				<br>
				<b>Risk and reward<br>
				</b>Banks are now bearing the brunt of heightened scrutiny and 
				public outrage over compensation that is believed to have 
				incentivised inappropriate risk taking. Regulators are expecting 
				banks to make risk management-related criteria a significant 
				part of how employee and business performance is assessed. 
				Rewarding individuals and business units for doing the right 
				thing can go a long way.<br>
				<br>
				<b>Proposals for reform<br>
				</b>Regulators, investors, governments and the general public 
				are demanding that banks reform existing governance models and 
				the ways in which institutional risk is managed.<br>
				<br>
				Most of the proposals address three key areas:<br>
				1. Enhance corporate governance<br>
				2. Reshape board organisation and operations<br>
				3. Increase engagement with regulators and shareholders<br>
				<br>
				We are entering a new and changing world. Executives, who show 
				ingenuity, have the courage to make tough decisions and 
				demonstrate the foresight to apply lessons from change will 
				guide their companies to success in the banking and capital 
				markets sector. And they will be the leaders who establish the 
				foundation upon which our new global economy will rise.</font><div align="center">
