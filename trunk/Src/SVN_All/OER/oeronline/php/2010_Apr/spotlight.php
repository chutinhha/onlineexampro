				more expensive than another tyre that appears to be a close 
				second, what can you do? The answer is quite simple – consider 
				evaluating your situation by comparing ‘how much per km’ each 
				tyre will cost. If you plan to drive your vehicle another 40,000 
				kms and are considering the ‘perfect’ tyres at 50 rials each, 
				and the other at 45 rials each; you may be surprised to find out 
				that the cost of the ‘perfect’ set costs just 1.25 baisas per km 
				while the close set costs 1.13 baisas per km. so the question 
				you will have to answer is “will saving the 20 rials today make 
				up for not having selected the perfect tyre’ that you will be 
				driving on for the next two years?</font><div align="center">
