				<b>Tyre safety tips</b><br>
				Never fit used tyres and change your existing tyres if they are 
				very old. Cracks in the tread or in the sidewall rubber, 
				sometimes accompanied by deformation of the carcass, are a sure 
				sign of aging. Have your old tyres checked by a tyre specialist 
				so that you can be certain they are still suitable for further 
				use.<br>
				<br>
				Do not use tyres with a tread depth of less than 1.6mm. Apart 
				from being illegal, tyres with low levels of tread will slide 
				easily, suffer from extended braking distances and will be more 
				susceptible to rupturing. In wet conditions the vehicle will 
				aquaplane making braking and handling almost impossible.<br>
				<br>
				Make sure your air pressures are correct. Low air pressure 
				generates heat which can result in the rubber and cord 
				separating, which in turn leads to the cord becoming cut and 
				puncturing the tyre. Low pressure also results in excessive wear 
				on the edge of the tyre, shortening the tyre’s life. Excessive 
				pressure, meanwhile, results in unpredictable handling in 
				addition to which, if the tyre suffers an impact, it’s easily 
				ruptured and cut. If you brake hard, the tyre may skid, which 
				reduces tyre life as the centre of the tread becomes worn. Check 
				tyre inflation pressures at least twice a month. Always check 
				pressures when tyres are cool and maintain the pressure 
				recommended by the vehicle manufacturer.<br>
				<br>
				Don’t drive with damaged tyres. If you drive with materials such 
				as stone in the tread groove, or with a nail stuck in the tyre, 
				the tyre can become punctured or ruptured. You should change the 
				tyre immediately on discovering any damage to avoid any further 
				damage to the carcass.<br>
				Treat abnormal tyre wear immediately. To assure normal wear you 
				should check air pressures and alignment and rotate the tyres 
				regularly. In addition you should avoid bad driving habits such 
				as sudden acceleration, braking and cornering.<br>
				<br>
				Check the spare tyre. Make sure you regularly check the spare 
				tyre’s air pressure, existence/non existence of damage and the 
				depth of remaining groove regularly.<br>
				<br>
				Never mount radial tyres on the same axle as non-radial tyres or 
				radial tyres on the front axle when non-radial tyres are mounted 
				on the rear axle. These conditions are likely to cause vehicle 
				instability resulting in a sudden loss of control and serious 
				injury.<br>
				<br>
				Check regularly that wheel nuts are securely tightened.<br>
				<br>
				Never overload your tyres. The maximum load and inflation 
				pressure of the tyres are moulded into the sidewall. Also, 
				remember; the tyre is capable of carrying the maximum load only 
				if it is inflated to its maximum air pressure.<br>
				<br>
				Do not use detergents or chemicals containing petroleum products 
				for cleaning or polishing your tyres.<br>
				<br>
				Avoid prolonged use on bad surfaces.<br>
				<br>
				If you get a puncture, stop as soon as possible and change the 
				tyre. Besides the obvious safety considerations, continuing to 
				drive on an under-inflated tyre can cause structural 
				deterioration. Punctured tyres must always be removed from the 
				wheel to check for secondary damage<br>
				<br>
				If it is necessary and feasible to repair a tyre, it must be 
				carried out by a tyre specialist as soon as possible to avoid 
				any structural deterioration. All tyre repairs must be entrusted 
				to a tyre repair specialist .<br>
