                <td height="18"> <b> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><font face="Verdana" size="1"> 
                <br>
                <br>
                </font><font size="4" face="Verdana" color="#CC3333">The 
			Sporting Edge</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<br>
                </font><font face="Verdana" size="2">With both the 2010 Range 
			Rover models out, Malcolm Xavier Crasta drives the new 2010 Sport to 
			see if it can steal our hearts away from its bigger sibling<br>
			</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                <br>
                Following in the heels of the revised 2010 Range Rover, comes 
			the new 2010 Range Rover Sport. Land Rover has taken a similar 
			approach with the 2010 iteration of the Sport as they have done with 
			the Range Rover. They have kept the external changes to a minimum 
			while optimising the driving dynamics of the vehicle on a whole.<br>
                </font></b> <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
			<br>
			The Sport has always been a handsome vehicle so why change it? 
			That’s most probably what the designers were thinking when they drew 
			up the new Sport. The changes made to the new model to distinguish 
			it from the older are quite subtle – major changes include the 
			revised two-bar grille, slimmer headlamps, new bumpers and that’s 
			it. But we have to mention that these and some other minor design 
			tweaks do make the car look slightly better than the old model.<br>
			<br>
			Inside the changes are a lot more prominent. There is a new dash 
			with easier-to-use controls and new door trims that make the 
			interior look even classier than it was. The dash mounted touch 
			screen seems to be lifted straight off the Jaguar XF, which is not a 
			bad thing, because it is one of the easiest systems to use on the 
			market. This also has the welcome side-effect of reducing the number 
			of buttons on the dash by about half. The instrument cluster also 
			sports a new LCD, in addition to the existing dials, that shows you 
			all the important information pertaining to the vehicle. <br>
			<br>
			The driving position hasn’t changed from the previous model and is 
			still great. It is comfortable, easily adjustable and offers 
			commanding views of your surroundings. And on the road it initially 
			feels a lot like its predecessor, smooth and composed, but it feels 
			more taut and balanced. The steering is sharper and more responsive 
			and the paddle shifters is a great addition that should come in 
			handy in off-road situations or for quick downshifts.<br>
			<br>
			ADDED DYNAMISM<br>
			A majority of the changes that make up the 2010 Sport are the engine 
			and major tweaks to the terrain response system. The Sport is now 
			powered by a 5.0L V8, good for 375hp and 510 Nm of torque. With so 
			much power under the hood it is no surprise how quick it can be. 
			While it may not be the fastest 4x4 out there, it is definitely not 
			slow by any standards and if you are so inclined there is always the 
			supercharged version with 510hp and 625Nm of torque. <br>
			<br>
			As for the Terrain Response System, new technology now takes it a 
			step further and includes a new Sand Launch Control system, which 
			prevents the wheels from spinning when moving off from standstill in 
			sand, and enhanced traction during rock crawl. With such technology 
			at hand, the Sport is near unbeatable in its class when it comes to 
			off-road performance, save for its bigger sibling. <br>
			<br>
			Another cutting-edge development is the introduction of Trailer 
			Stability Assist, which identifies trailer sway to trigger active 
			braking and cuts engine output to stabilise the trailer. Not that 
			this will come for much use in Oman but it is nice to know that its 
			there if you need it.<br>
			<br>
			While the design changes may not be massive, the changes to the 
			interior and performance, on and off the road, make this vehicle 
			quite an improvement over last year’s model. And were it not for the 
			2010 Range Rover, the Sport would have been a no brainer. But since 
			both are available the choice will not be easy. Personally, we like 
			the Range Rover ever-so-slightly more than the Sport, so that would 
			be our choice but you can’t go wrong with picking the latter either.
