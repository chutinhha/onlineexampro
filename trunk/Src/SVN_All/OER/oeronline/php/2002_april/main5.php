                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Success management&nbsp;</font><p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/oman1.jpg" align="left" hspace="5" vspace="5" width="250" height="187"></font><font face="Verdana" size="1"><b>Everyone desires success. But true success cannot be gauged by monetary gains alone. It is a conglomeration of physical health, mental peace and a positive outlook that helps people function better and more effectively, says George F Grossniklaus</b></font><br>
                    <font face="Verdana" size="2">
                    <br>
                    </font><font face="MS Sans Serif" size="2">The secrets of success are freshness and mental agility. Not money! What is wealth worth, if humans miss the ability to divide it correctly and to administer their properties appropriately?&nbsp;<br>
                    Humans, who depend in the struggle for existence only on money and greed will obtain no long lasting success, they will instead become intrinsically poor.<br>
                    Garnborg, the Swedish writer, answered the question on what money can buy, with the following enriching words:&nbsp;</font></p>
                    <ul>
                      <li><font face="MS Sans Serif" size="2">Meals, but no appetite&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Medicine, but no health&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Scientific interest, but no intelligence&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Glitter, but no taste&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Scattering, but no mental satisfaction&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Acquaintance, but no friends&nbsp;</font></li>
                      <li><font face="MS Sans Serif" size="2">Servants, but no loyalty&nbsp;</font></li>
                    </ul>
                    <p><font face="MS Sans Serif" size="2">The wrappings of all the beautiful things of life, one can acquire with money, but never the core. Do you understand now based on these examples, how you would be able to become really lucky? By means of an internal balance, clarity, harmony and peace. You must keep yourself away from self-centred and jealous emotions; you must become generous, completely free and internally relaxed.&nbsp;<br>
                    Then you can go confidently and straight to work, to shape your success story. Walk the forward path of the courageous ones, energetic, persevering as well as the active. Say to yourself that life and success need to be fought for. Achieve a lead in the mental and practical areas over your fellow men, only then you are on the way to real success. There is no advantage without effort. Without pain no gain!<br>
                    <br>
                    <b>Your relationship with your own self</b><br>
                    You must learn to handle yourself. Practise the internal freedom, because fleeing from it is almost unpardonable. Beware of neglecting your own person, because you are your best and steadiest friend, if you take care of yourself. If you want to find luck and success, then you must handle yourself fairly and do not spoil yourself by strong self-pity. Examine the health of your soul and body, but please no effeminacy.&nbsp;<br>
                    Do not let yourself to be torn between passions. If you feel unwell, or are mentally depressed, then do not entertain gloomy thoughts. Try to seek ways of self-mastering, overcoming your sorrows.<br>
                    <br>
                    <b>The inner critic</b><br>
                    Our heads are filled with all sorts of noise. We talk to ourselves, we think about other things when our attention should be focused on the task at hand, and we worry incessantly. By some estimates, the human brain thinks of an average of 400 to 600 words every single minute. And often, what we tell ourselves is negative; we'd be insulted and hurt if someone else said such things about us. Our inner critic - the nasty little voice in our head riding us down - can get pretty brutal:<br>
                    <br>
                    &quot;I can't do it&quot;<br>
                    &quot;I'm a failure&quot;<br>
                    &quot;Why try?&quot;<br>
                    &quot;They don't really like me&quot;<br>
                    &quot;Just give up&quot;<br>
                    <br>
                    Many of us hear these thoughts over and over again, day after day. It can add up to a tremendous drawback to our self-esteem and confidence. Imagine having your boss talk to you like that all day. You'd feel pretty demoralised and angry by the end of the day. And you can't leave your inner voice at the office . . .&nbsp;</font></p>
                    <p align="center"><font face="MS Sans Serif" size="2"><img border="0" src="../../images/2002_april/p-50.jpg" width="400" height="230"></font></p>
                    <p><font face="MS Sans Serif" size="2"><b>Positive reviews for positive thinking</b><br>
                    Fortunately, we can train ourselves to change negative messages into positive thoughts. It may sometimes seem as if you can't control that inner voice, but remember, it's your brain! While no one can completely eradicate negative thoughts, we can learn to substitute healthier, empowering thoughts for many of our defeatist messages. If you make positive thinking a habit, you'll find there isn't much room left for the gloomy attitude.<br>
                    Our subconscious is an odd place: it can't tell the difference between what we think and what we do. It tends to believe whatever we tell it. It listens to our thoughts and takes its cues from them, so it's tremendously important that we take control of what we feed into it. It takes practice to monitor our own thoughts, but after a while, it's fairly easy to change a I can't" into "I can" or a "I failed," into "I learned from that experience".<br>
                    <br>
                    <b>A positive attitude spills over</b><br>
                    You'll be surprised at how contagious a positive attitude can be.&nbsp;<br>
                    If positive thinking becomes a habit, you'll find that the things you say spontaneously will also be positive. If you are having trouble developing a positive attitude, practice thinking what you would say to your sister or your best friend if they were telling you about their negative thoughts. How would you cheer someone else up? You deserve to spend at least as much energy doing this to yourself.<br>
                    Respect yourself if you want others to respect you. Never do anything over what you have to be ashamed of later. Do not envy the luck and success of your fellowmen. Do not criticise strangers, before you know your own errors and weaknesses.<br>
                    Never say: &quot;I am better than others,&quot; but show what you really can do. Strive for perfection and excellence, but never stress on your infallibility. Always be aware of yourself, and never lose the courage, the self-assurance, and search for good human beings. Furthermore, be alert in every situation, so that you can win. Be straightforward and keep your word. Fickleness and white lies do not lead to mental peace. Be consistent in your character, so as not to be friendly and polite today, but blunt tomorrow.&nbsp;<br>
                    First of all, do not degrade your fellowmen, do not publicise their weaknesses in order to be one upon them. Next, make a difference between sincerity and openheartedness. Be a pleasant company, and do not ginger up your discussions with insulting and slanderous expressions. Maintain your moral strength in each situation.&nbsp;<br>
                    Keep your desk in order, and never throw anything randomly. Accuracy means profit for you in time. Success is not a coincidence - it can be calculated, planned and compiled.&nbsp;<br>
