                    OPEC oil ministers stood firm when they met in Vienna in mid-March, resisting pressure to boost oil production in response to rising prices. They virtually ruled out production hikes for the months to come by pledging to stick to current output quotas until crude hits US$28 per barrel.<br>
                    “We have a range between US$22 and US$28 for oil prices,” OPEC Secretary-General Rilwanu Lukman told reporters during the meeting. “So why should we raise (output) if it’s not above US$28?”<br>
                    Prices topped US$24 per barrel in the run-up to the meeting, on March 14, as US President George Bush stepped up his anti-Iraq rhetoric. His comments increased the likelihood that America will launch a military attack against Saddam Hussein late this year or early next - a move that will almost certainly lead to Iraq turning off the taps at its oil wells.<br>
                    Despite this short-term price optimism, OPEC ministers take the view that the fundamentals of the demand-supply balance - discounting what they describe as a US$3 per barrel Iraqi “war premium” - remain weak. So they voted to keep the cartel’s production quota at 21.7 million barrels per day, the lowest for ten years and leaving more than five million barrels per day of spare capacity among OPEC nations.<br>
                    Oil markets have made a dramatic recovery in recent months. In November 2001, the price of crude dipped to around US$17 per barrel as the post September 11, 2001 economic pessimism caused analysts to sharply downgrade forecasts of oil demand. OPEC was forced to abandon its US$22 per barrel target price floor and seek an agreement with key non-OPEC producers such as Russia and Norway to slash supply in a bid to prevent a price collapse.<br>
                    However, with the US seemingly recovering quicker than expected, and international aviation picking up, oil demand prospects have improved. This is good news for oil dependent OPEC heavyweight such as Saudi Arabia, which slashed government spending by 20 per cent as oil prices nosedived in late 2001. But the Saudis clearly do not feel that they are out of the woods yet, so are inevitably taking a cautious approach to any possible output hikes.<br>
                    “The economic growth is just starting,” Algerian oil minister and former OPEC president Chakib Khelil told journalists in Vienna. “My hunch is that I don’t foresee sufficient economic growth such that we will need to do any increase in June.” Others agree. “I see no reason now to boost production in June,” said Qatari oil minister Abdullah bin Hamad al-Attiyah.&nbsp;<br>
                    Non-OPEC compliance adds further uncertainty to the situation. Russia, Norway, Mexico, Angola and Oman all agreed to help OPEC lower supplies as of January 1, 2002. Russia, the biggest oil exporter outside OPEC, has supported the group with a promise to trim exports by 150,000 barrels daily during the first quarter of this year. But OPEC knows this support is fragile.&nbsp;<br>
                    Norway, the world’s third-largest oil exporter, has reiterated support for the OPEC-led effort to prevent prices from falling during the second quarter, seasonally the weakest period for demand. “We will continue with output cuts until the end of June,” said Ingvild Haug, an oil ministry spokeswoman. “We have no plans to raise output now.”&nbsp;<br>
                    But analysts fear Russian support will be far less clear-cut. Under these circumstances, and with the US enjoying one of its mildest winters on record, many think OPEC is well-advised to tread cautiously.<br>
