				<font face="Verdana" size="1">His Excellency Mohammed 
				Nasser Al-Khasibi, Secretary General of the Ministry of National 
				Economy and Chairman of ITA and TRA unveiled Progress, the 
				prestigious annual publication from United Media Services (UMS). 
				His Excellency was happy to launch Progress, especially as it 
				coincides with the celebrations for the 40th Renaissance day. 
				Speaking on the occasion His Excellency said, “This book 
				highlights the achievements in the Sultanate over the past forty 
				years and will be an important reference point for researchers 
				who wish to know about what has been achieved during this 
				period. Progress documents the Sultanate’s achievements through 
				letters and images and is sure to enrich Oman’s library.”<br>
				<br>
				The unveiling ceremony was attended by Sandeep Sehgal, Chief 
				Executive, UMS; Alpana Roy, Executive Vice President, UMS; 
				Mayank Singh, Group Editor, UMS; Khalfan Al Rahbi, Deputy 
				Editor, Alam Aliktisaad Wala’mal and Talal Al Rahbi, Chief of 
				Information and Awareness Division, ITA. Says Sehgal, “Progress 
				has chronicled the Sultanate’s social and economic growth for 
				the last 16 years. On the 40th anniversary of the Blessed 
				Renaissance, we humbly dedicate this year’s edition to the 
				visionary leadership and guidance of His Majesty Sultan Qaboos 
				bin Said.”<br>
				<br>
				Progress 2010-2011, takes a look at 40 landmark achievements 
				that have defined the Sultanate’s march towards development, 
				modernisation and prosperity. These pages give a bird’s eye view 
				of Oman’s transformation from an underdeveloped and isolated 
				country to a developed and modern nation. This year’s edition 
				also stands out for its exhaustive coverage of various sectors 
				like Oil and Gas, Banking and Finance, Capital Markets, 
				Education and Healthcare, Insurance, Agriculture, Retailing, 
				Aviation, Information Technology, Real estate, Telecom, 
				Manufacturing, Power, Infrastructure and Tourism. Apart from top 
				draw articles, this edition of Progress stands out for 
				contributory pieces from a number of ministers.<br>
				<br>
				HE Ahmed bin Abdulnabi Macki, Minister of National Economy and 
				Deputy Chairman of the Financial Affairs and Energy Resources 
				Council says, “Significant economic achievements have been made, 
				mainly by adopting prudent economic management of the oil wealth 
				to improve the macroeconomic fundamentals, strengthening the 
				financial system, and advancing the economic diversification 
				programme.”<br>
				<br>
				HE Maqbool bin Ali Sultan, Minister of Commerce and Industry 
				writes, “The Ministry is trying its best to accomplish its goals 
				in all crucial sectors such as commerce, industry and minerals.”<br>
