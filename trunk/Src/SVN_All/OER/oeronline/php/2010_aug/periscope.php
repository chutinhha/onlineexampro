					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					The coming rally in emerging markets!</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2"><br>
					Strong fundamentals and a 
					low downside risk make emerging markets an attractive 
					proposition. A composite fund that invests across countries 
					is an option that investors should look at seriously<br>
&nbsp;</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">Emerging markets have swooned 
					since late April when US, European and Japanese equities 
					peaked in a classic global growth scare. Economic softness 
					in the US, Europe and China is undeniable. US private 
					payroll growth and new home sales data suggests that a 
					V-shaped recovery is a cruel illusion, that the economic 
					supertanker has hit a soft patch. However, financial markets 
					extrapolate the mass neuroses of the recent past, so it does 
					not surprise me that US equities price a double dip 
					recession. Why else is the 10 year Treasury bond yield a 
					mere three per cent and the S&amp;P500 index trading at a modest 
					12 times forward earnings, at least 250-300 points below the 
					last decade historical multiple range?<br>
					<br>
					<b>The Chinese factor</b><br>
					<br>
					The Chinese squeeze is all too real. Bank loan growth, 
					industrial production, power consumption and car sales 
					definitely indicate that the Middle Kingdom’s GDP will grow 
					at eight to nine per cent, far below the 11.6 per cent first 
					quarter peak. Europe’s fiscal austerity, enforced by German 
					Chancellor Merkel and the interbank funding markets, has 
					just begun. Japan is still mired in deflation and the new 
					DPJ Prime Minister Kan faces a succession of political 
					crises. <br>
					<br>
					India and South Korea are two major emerging markets whose 
					central banks raised interest rates to combat an inflation 
					surge. Oil has fallen $15 from its highs. Copper has lost 
					one fourth of its value since April. The Chinese Politburo 
					is determined to pop the property bubble and the Beijing 
					central bank has not eased monetary policy. The Shanghai 
					stock index has lost 20 per cent since January. The macro 
					tea leaves hardly argue for a bull run in the emerging 
					markets.<br>
					<br>
					However, I believe a near term bottom in the asset class is 
					imminent. The Morgan Stanley Emerging Market Index Fund 
					(symbol EEM on the New York Stock Exchange) has fallen from 
					46 to 37. This is an ideal index fund because it enables 
					investors to put money to work in the six leading emerging 
					markets in the Morgan Stanley index, (Brazil, South Korea, 
					China, Taiwan, India, Russia) across a broad spectrum of 
					blue chip companies. While disappointments in the US 
					earnings calendar, a hard landing in China and external 
					shock (credit black swans like a Greek sovereign default?) 
					will definitely hit EEM, I doubt if downside risk is more 
					than 20 per cent from current levels. At 33-36, this index 
					fund enables investors to gain exposure to the world’s 
					fastest growing, least leveraged, most youthful economic 
					bloc at a valuation multiple that is below 10 times 
					earnings. This is my definition of long term value and 
					successful investing.<br>
					<br>
					There are myriad catalysts for EEM to move 20-25 per cent 
					higher to my target of 50-52 in the next six month. One, 
					since investor sentiment is priced for deflation, the 
					snapback will be huge when the markets scramble to buy beta, 
					risk and asset reflation themes – fancy Wall street jargon 
					for emerging markets.<br>
					<br>
					Two, the IMF estimates 4.6 per cent global GDP growth. This 
					is nowhere near global recession. Three, crude oil still 
					trades in the $70-75 range without a new OPEC oil cut. In a 
					global recession, black gold would have plummeted to $20. 
					The Australian dollar, a pure play on commodities and China, 
					trades near its recent highs at 0.86-0.88 cents. The smart 
					money pooh poohs the deflation argument. So should you.<br>
					<br>
					Three, there is no doubt that the Federal Reserve, the ECB 
					and the Bank of Japan will dare raise interest rates as long 
					as global banking remains haunted by systemic risk. Central 
					banks act as lenders of the last resort during financial 
					crises, not anti-inflation vigilantes. In any case, American 
					inflation is falling. Zero interest rates mean the world’s 
					top three central banks act as planetary monetary pumps. Who 
					are the ultimate beneficiaries of zero dollar interest 
					rates, underweight fund managers and a global liquidity 
					surge? Emerging markets.<br>
					<br>
					<b>Place your bets </b><br>
					<br>
					My favourite emerging market is now Russia, whose forward 
					valuation is a mere six times earnings at a time when the 
					Kremlin has floated its first sovereign Eurobond since 
					Yeltsin’s August 1998 rouble devaluation. Incredibly, Russia 
					trades at a 40 per cent discount to both Brazil and China. I 
					am reluctant to invest in India at Sensex 17000-18000 since 
					Dalal Street is one of the most expensive markets in the 
					world at a multiple of 16, the Reserve bank of India (RBI) 
					is behind the curve in monetary tightening and food 
					inflation means political risk for Congress, as the recent 
					strikes proved.<br>
					<br>
					Turkey is not expensive at 11 times earnings but the lira is 
					hostage to foreign capital and political risk is rising with 
					the next general elections and the war with the PKK Kurdish 
					rebels in Anatolia. I believe China is finally cheap at only 
					14 times earnings for 20 per cent profit growth and the 
					recent revaluation of the yuan both averts a protectionist 
					backlash in Washington and ignites the economy of 1.3 
					billion consumers. China will not aggressively tighten 
					because the Politburo does not want or need a hard landing. 
					Emerging markets are cheap, unloved and underowned. This is 
					the potential winner asset class of 2011.<br>
