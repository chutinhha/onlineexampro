					explains that the company’s administration and Logistics 
					team have enough capacity and skills to support its 
					operation. “Our camps, workshops, and offices spread all 
					over Oman in north, central and south Oman to support all 
					our operation activities. We have specialist team that look 
					after all our equipment and fleet,” he adds.<br>
