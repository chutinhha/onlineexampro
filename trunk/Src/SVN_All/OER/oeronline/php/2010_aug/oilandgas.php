					in the HR department handling a variety of jobs including 
					initial processes of recruitment, assisting the HR manager 
					for selection and other HR related matters.<br>
					<br>
					As an Omani company, AL-GHALBI is totally committed to meet 
					the Sultanate’s Omanisation policy requirements and its 
					policy is to recruit Omanis with relevant qualification from 
					the university, college or an experienced candidate from the 
					oilfield services. The company has already exceeded its 
					Omanisation targets and it complies with OPAL standards.<br>
					<br>
					“I would like to say that, AL-GHALBI is providing very good 
					opportunities to Omani women employees and treats them with 
					dignity, so much so that many times I feel I am working with 
					my own family members,” adds Al Habsi.<br>
