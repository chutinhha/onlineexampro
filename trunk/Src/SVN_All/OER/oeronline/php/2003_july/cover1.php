            People First - </font><b><font face="Verdana" size="2">Maintaining 
            the Standard<br>
            <br>
            </font></b><font face="Verdana" size="1"><b>Louise Bower, international 
            development manager for IiP UK, The International Development 
            Consortium, talks to Sue Hutton about its search for an Omani 
            partner, the processes which are involved in IiP and the reasons why 
            local organisations should get involved </b> <br>
            <br>
            <b>
            <img border="0" src="Cov-Louise.jpg" align="left" hspace="3" vspace="3" width="175" height="212">A recent newsflash on IDC's website explains that it is currently 
            in discussions with a potential partner in Oman. Who is that 
            potential partner likely to be? </b><br>
            <br>
            It's likely to be a government department or agency in Oman. There 
            are two or three different avenues. British Council has spearheaded 
            the IiP initiative in Oman and been largely responsible for getting 
            it up and running, but the IiP partner will be Omani who is able to 
            deliver IiP in a way that suits the Omani marketplace. <br>
            The feedback we've had from Oman and Dubai is that the companies 
            that have become Investors in People there, of which there are still 
            only a handful, really are quite exemplary. </font>
            <p><font face="Verdana" size="1"><b>You say that IiP began as a 
            small initiative within a government department in UK. How might 
            this translate to the situation in Oman in regards to an agency 
            developing the IiP Standard?</b><br>
            The agency would have to be committed to ensuring that the Standard 
            is upheld correctly. They would be able to develop their own 
            supporting materials, so in terms of how they explain or promote or 
            deliver or charge for delivery of the Standard, they would be able 
            to do that in their own way. [The International Development 
            Consortium] will be acting on behalf of IiP UK and will have an 
            ongoing quality management relationship with them to ensure that the 
            Standard is maintained properly. <br>
            <br>
            <b>Do assessors interview every employee or only meet with managers?</b>
            <br>
            They won't interview every employee, but before they begin the 
            assessment, they will get an overview from the HR director, 
            probably, about the nature of the business, the different business 
            units, the number of employees, where employees are sited, what 
            employees do, and the length of service of employees. The assessor 
            will then identify a representative sample. They will make sure that 
            that the sample includes representatives from cleaners to the chief 
            executive. <br>
            <br>
            <b>What can assessors gain from this? </b><br>
            They would write their assessment report in a way that meets the 
            specific needs and requirements of the company. The assessor will 
            ask 'Where do you think your strengths and weaknesses lie?' and 
            'What do you want to get out of this assessment?'. The company will 
            hopefully get positive recommendations and a copy of the assessor's 
            report. Even if the company is successful in meeting the IiP 
            Standard, it will still get a report highlighting the strengths and 
            weaknesses that the assessor has identified. If the company is not 
            successful, the report will explain why and identify the areas that 
            the company needs to work on. The report will go to management, not 
            individuals, so it is up to the management to ensure that 
            recommendations are acted on. <br>
            <br>
            <b>A concern expressed by Omani organisations is that meeting the 
            IiP Standard will be fairly expensive. Is it?</b> <br>
            Yes, at the moment it is more expensive than it needs to be because 
            it is delivered from UK. The whole idea of setting up a national 
            partnership in Oman is that it will, aside from the language and 
            cultural issues, then become cheaper to deliver than to keep 
            bringing people out from UK. Unfortunately, the only way to get 
            those people trained and developed in Oman is to bring the expertise 
            out from UK in the first place. So it will become cheaper.<br>
            In those countries where IiP is delivered locally, costs are 
            different. They will be different in the Czech Republic for example, 
            than from UK. Costs will also depend on the availability of training 
            resources. Locally available training will bring costs down.<br>
            From our point of view, once we get through the pilot stage, and 
            they're developing their own support, assessment and advice, it's 
            for the local partner to charge what the market will bear. What we 
            will do as the organisation for international quality management of 
            the IiP Standard, is to make sure that small businesses aren't being 
            priced out of the market. <br>
            Now clearly, other countries aren't likely to have the same kind of 
            subsidy system that we have in UK to enable small businesses to do 
            IiP, but we will be working with the local partner to look at a 
            pricing structure that will, for example, charge more than the going 
            rate for large companies so that they can charge less than the going 
            rate for small companies. <br>
            The really important thing for companies to know is that the only 
            thing that they have to pay for is the assessment. They can prepare 
            for assessment within the company wit.hout necessarily bringing in 
            specialist expertise. <br>
            <br>
            <b>Another concern in Oman is that it could be too time-consuming?</b><br>
            It can be. The important thing is that IiP is flexible enough that 
            it doesn't mean that a company has to change the way it is doing its 
            business. It's designed for organisations to fit their own way of 
            doing business in such a way that they can achieve the outcomes that 
            IiP defines as what they need to achieve. It doesn't have to be 
            time-consuming in the sense of developing new processes, but it will 
            depend on the organisation. For a big organisation, it might take up 
            a lot of time and resources. In a small company it may not. It 
            depends where they're starting from.<br>
            <br>
            <b>Is there a lot of paperwork involved? </b><br>
            There doesn't have to be. It fits around what the company does 
            already. If the company generates a lot of paperwork and the way 
            they communicate is through paper, then that's what they do, but if 
            they're a &quot;paperless office' there's no need to start creating a lot 
            of paperwork. <br>
            <br>
            <b>Is IiP mandatory in the UK and will it be a mandatory requirement 
            in Oman? </b><br>
            No, IiP is not mandatory in the UK, except that the British 
            government has said that all central government departments should 
            be Investors in People. That's the government telling its own 
            offices what they want. I don't know of anywhere other than the UK 
            government where IiP is mandatory. If the government of Oman decides 
            to make it mandatory, that is their choice, but we certainly 
            wouldn't put that condition on it. <br>
            <br>
            <b>In Oman, where there is a huge push towards Omanisation, how does 
            IiP fit?</b><br>
            What IiP is not designed to do is to come into conflict with 
            employment legislation. One of the reasons that we start pilot 
            schemes in places like Oman is to explore how those kinds of 
            policies fit in with IiP. We had to look at ways to implement IiP 
            that didn't cut across that, so if national legislation in Oman says 
            that there isn't any training budget available for training and 
            developing expatriates, we would have to look at ways to ensure that 
            IiP wouldn't come into conflict with that. <br>
            It's about what's best for the business, so if you have an 
            expatriate in position, you want them to be motivated. Maybe the 
            priorities are different, but you still don't want that person 
            sitting there, not achieving his best. </font></p>
