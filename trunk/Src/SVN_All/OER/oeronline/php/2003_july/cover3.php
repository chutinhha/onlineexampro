            People First -</font><font face="Verdana"><font size="4" color="#FF0000"> </font> </font>
            <b><font face="Verdana" size="2">Aiming for the Standard </font></b><font face="Verdana" size="1">
            <br>
            <br>
            <b>Organisations in Oman that are striving to achieve Investors in 
            People status know that they still have many hurdles to cross. For 
            some, these challenges will reap many benefits, for others, they may 
            not. OER reports</b><br>
            <br>
            It began as a pilot scheme here just over a year ago and its early 
            results already prove that employers in Oman can achieve great 
            success with Investors in People (IiP). Having said this, even those 
            that didn't achieve the Standard immediately say they are already 
            appreciating the benefits of working towards the IiP Standard. <br>
            Elghifari Ibrahim Hussein, head of training, BankMuscat, says, 
            &quot;Actions are being taken to ensure that all the training functions 
            undertaken have a direct relevance and impact on the business of the 
            bank rather than training for the sake of training. This helps the 
            training department to project itself as a business partner to other 
            units in the bank, and also to extend the scope of training service 
            to internal consulting and being a major change agent within the organisation.<br>
            BankMuscat, he explains, wished to be assessed against the national 
            standards for its own development purposes and to benchmark itself 
            against other similar organisations worldwide. Based on the initial 
            assessment at the bank where 44 staff were interviewed at random as 
            well as groups of staff, an action plan has been formulated to 
            bridge all the identified gaps. The bank will go for formal 
            assessment during the second half of the year 2003. <br>
            &quot;A HR task force has been formed to administer the implementation of 
            the Investors in People across BankMuscat, designed to adopt a 
            step-by-step 'building block approach' rather than assessing the 
            entire organisation at one time. We will learn from the process and 
            carry it further to other areas of the bank until all the branches 
            and departments are covered,&quot; he adds. <br>
            Another organisation on the ongoing pilot project that is 
            introducing the principles and protocols of the IiP Standard as an 
            action plan using this building block approach is Oman Refinery 
            Company (ORC). A spokesman there admits that there is always more 
            that could be achieved in HRD (human resource development) but that 
            everyone wins when there is full commitment. He is confident that 
            although exact dates have not been finalised, the company remains 
            hopeful that certain sectors of the company will be ready to submit 
            for an assessment by end of the year as well. <br>
            &quot;It would be nice if one could simply switch off the old habits 
            overnight and transport our staff to a new set of doing things. That 
            just does not happen. Of course, I would like to think everyone is 
            giving as much as is possible to achieving the Standard, but I know 
            we could do better and we in HRD are giving this a high priority 
            because we know it is good for ORC and our employees,&quot; says Said S. 
            Al Aufi, operation development officer, ORC.<br>
            Investors in People themselves lists as benefits improved 
            competitive edge, improved productivity and improved profits, among 
            many others. Surveys have indicated that IiP organisations offer a 
            very high job satisfaction, for example. &quot;We hope to significantly 
            improve communication and the way we involve our staff in their 
            development. We also hope to focus our training more towards 
            business needs, get more from our investment,&quot; adds Al Aufi. <br>
            It cannot guarantee to solve everyone's problems, but all 
            organisations, whether involved in manufacturing or supplying 
            services need to keep their customers happy and to do this, they 
            must ensure that they are meeting their needs successfully. To do 
            all this, all organisations need their people - from their shop 
            floor to their boardroom - to do a good job and produce quality 
            work. Indeed, the IiP project is charged with bringing about a 
            skills revolution in Oman by urging local employers to take 
            investment in people as seriously as they would take investment in 
            plant and equipment.<br>
            Having said this, the jury is still out on whether all large 
            manufacturing firms and government organisations in Oman could 
            implement such a standard practically. &quot;Unlike the service sector 
            that relies upon human capital, the cement industry is a highly 
            capital and machinery intensive sector, but one still can't 
            underestimate how investing in people will deliver tangible 
            results,&quot; says Najim M. Al-Timami, general manager of resources and 
            services, Oman Cement. <br>
            Many companies have expressed a firm interest in attaining the IiP 
            standard but for some companies, such as Oman Cement, which have 
            spent considerable time researching the benefits of IiP, the scheme 
            may not suit at this time. Al-Timami explains that it already 
            follows stringent HR protocols and has adopted investment in people 
            for a long time. During 2002, it laid special emphasis on training 
            for Omani employees. In fact, 51 nationals were sent abroad for 
            training while in-house training for another 143 employees was 
            conducted where specialised instructors were invited to Oman from 
            overseas.<br>
            &quot;No company can ignore its investment in people. We are impressed 
            with what Investors in People can offer. With a substantial payroll 
            cost for 370 employees, it is important to make sure that you are 
            paying the right people right. Motivating them is a cornerstone for 
            success,&quot; he adds. <br>
            There are no hard-and-fast rules about what motivates an employee, 
            believes Al-Timami. A particular incentive interests an employee 
            differently depending on when it is being offered during their 
            career. Salary and other perks including end of service benefits or 
            pension schemes, job challenge and satisfaction, career development 
            prospects, training and exposure opportunities, recognition, praise, 
            support, working environment, holidays, and job title are among 
            things that matter. Many seemingly unimportant things like a 
            business card or even a mobile phone to junior employees may also 
            make them tick. &quot;Creating win-wins can cost very little,&quot; he 
            stipulates.<br>
            Siva Reddy, finance controller of National Training Institute, 
            another organisation involved in the IiP pilot, says that the 
            introduction of such a Standard in Oman is good news, but more needs 
            to be done to create a conducive quality labour market in the 
            Sultanate, a pre-requisite for best practices of HRD. <br>
            Reddy says, &quot;Employees should be given opportunities for better 
            representation which, in turn, paves the way for two-way 
            communication with the employers that is needed for better HRD. Open 
            lines of continuous communication are needed in order to initiate 
            meaningful measures for improved employee and employer relations,&quot;<br>
            <br>
            <b>What lies ahead? </b><br>
            Still, the future for Investors in People looks bright. The next 
            phase of IiP development in Oman, which has been running 
            concurrently with the pilot project, is to cultivate further local 
            participation from government and the local business community. 
            During the past nine months, discussions have been taking place 
            between IiP representatives and senior government officials while 
            many other private sector organisations - such as the Al Bustan 
            Palace hotel, divisions of the WJ Towell Group including Matrah Cold 
            Stores LLC, Mustafi Sultan Enterprises, Al Ansari Trading 
            Enterprises and even manufacturing and industrial heavyweights such 
            as Galfar, Oman Cables and Oman Cement - have expressed an interest 
            in the IiP standard. Many of them could get involved before the end 
            of the year.<br>
            In the interim, IDC will provide support services to concerned local 
            IiP participants. Sue Tappin, representing IDC locally, suggests 
            that a precise roadmap for the Standard in Oman will become much 
            clearer after the summer. During this time, H.E. Juma bin Ali al 
            Jumaa, the Sultanate's minister of manpower, will be visiting the UK 
            where he will meet with UK government officials that are directly 
            responsible for education skills, training and IiP. He will also 
            meet with the senior management of IDC. <br>
            &quot;I think you will see that the Ministry of Manpower will find the 
            Investors in People framework to be a powerful manpower-planning 
            tool. The Ministry of Commerce and Industry and the Ministry of 
            Education have also expressed an interest to get involved with the 
            framework. In the UK, the government is encouraging all ministerial 
            departments to work towards the Standard to ensure all civil 
            servants are effectively trained and developed to offer an effective 
            service,&quot; explains Tappin. <br>
            <br>
            <b>An Omanisation driver?</b><br>
            Investors in People is about equal opportunities for all employees 
            in regards to gaining access to training, not prejudiced by their 
            role in the organisation, their gender or nationality. Of course, 
            Omanisation is the national priority, but there are realistic ways 
            of looking at HR management. For example, some organisations in the 
            pilot project were worried that they would be penalised for offering 
            specialised courses abroad to Omanis but not to expats. <br>
            Not so, explains Tappin. &quot;The expatriates brought into these 
            particular companies are normally experts in their own right and 
            have been employed for their skills and therefore it would not been 
            an issue to focus instead on those who would better benefit from 
            this to enable to do their jobs more effectively.&quot; <br>
            IiP is about ensuring your workforce is able to achieve your 
            business objectives - so it is about ensuring only those staff that 
            need to be developed are identified and are supported through a 
            learning experience to ensure they are able to do their jobs 
            effectively. However, it is in any organisation's interest to ensure 
            their workforce - whether nationals or expatriates - are competent. 
            This has been seen to be the case in all of the pilot group 
            organisations which involve their expatriate workers in in-house 
            training and updating events. <br>
            &quot;Another benefit from establishing Investors in People in Oman would 
            be that the organisation could add new standards along with existing 
            ones such as recognising succession planning for Omanisation and 
            knowledge management (where expertise is transferred efficiently),&quot; 
            says Guy Thomas of the UK-based International Development Consortium 
            (IDC). <br>
            IiP also gives equal opportunities for progression. If there are 
            gaps created within the company, there should be equality of 
            opportunities. As people receive training and gain promotion, a 
            broad range of middle management could become Omanised, thus freeing 
            up lower level jobs for new entrants to the workplace. There are 
            prejudices within companies as there are around the world, but the 
            challenge will be for the management to be what the government 
            policy is of the country and what is best for their organisation in 
            that country. <br>
            &quot;I think we must not lose sight of the goals of the Investors in 
            People programme - that all employees are focused on meeting the 
            organisation's objectives. Whether there is a conflict or not with 
            the progression of individuals within the organisation, the 
            important thing to consider is that people are performing at their 
            most optimum level - whoever they are,&quot; adds Thomas.<br>
            Dr Sunil Gupta, of IDEAS Management Consultants in Oman, knows that 
            the challenge is not only to equip employees here with the skills, 
            knowledge and motivation they need to create organisational systems 
            that support high performance, but to convince employers that 
            perhaps their management focus needs fine-tuning to deliver results 
            ultimately to the bottom line of the business. <br>
            &quot;Inevitably, the Standard will lift a few veils that an organisation 
            had not wanted to look behind but they must view this process as 
            positive. They will be forced to confront a number of issues and 
            install best practices and protocols. But all of this will 
            eventually help them to improve their P-S-P (People, Service, 
            Profits) formula,&quot; he says.<br>
            &quot;Oman's government is working hard to find a balance between finding 
            jobs, speeding up induction of new workers and delivering training. 
            In my opinion, Investors in People will indirectly stimulate 
            Omanisation through providing a structured framework for all aspects 
            of HRD,&quot; says His Highness Faisal Bin Turki Al-Said, head of 
            planning and follow up, OCIPED. <br>
            &quot;In the future, I would personally like to see Oman having its own 
            national Standards and that the programme be tailored to the 
            requirements of the local industry and economy,&quot; he adds.<br>
            The time is right for the Sultanate to contemplate such an HR 
            approach. It is not lagging behind - in fact, Oman is at the 
            forefront of developing Investors in People.<br>
            Yet, a different method is needed since as OCIPED reminds us so 
