            </font><b><font face="Verdana" size="1"><br>
            </font></b><font face="Verdana" size="1"><b>Sue Hutton talks to Ruth 
            Spellman, chief executive, Investors in People UK, about the 
            progress the IiP Standard has made and how it can improve business 
            performance and competitiveness</b> <br>
            <br>
            <b>
            <img border="0" src="cov-RUp.jpg" align="left" hspace="4" vspace="4" width="175" height="216">Why was the IiP Standard introduced initially? </b><br>
            The Standard was developed during 1990 in the UK by the National 
            Training Task Force in partnership with leading national business, 
            personnel, professional and employee organisations such as the 
            Confederation of British Industry (CBI), Trades Union Congress (TUC) 
            and the Chartered Institute of Personnel and Development (CIPD). <br>
            <br>
            <b>What is IiP? </b><br>
            Investors in People is the national Standard which sets a level of 
            good practice for improving an organisation's performance through 
            its people. It is about results - not procedures. It is a practical 
            tool for the continuous improvement of both the organisation and its 
            people. <br>
            <br>
            <b>How did ISO 9000 come to be regarded as inadequate as the sole 
            standard for effective quality management of an organisation? </b>
            <br>
            I disagree with the statement regarding ISO 9000's effectiveness. It 
            is important to remember that ISO 9000 is designed to link business 
            effectiveness to business objectives and now also incorporates 
            management and people responsibilities. This complements the work of 
            the Investors in People Standard which is a framework for linking 
            people development to business planning. In fact, many people hold 
            both the Investors in People Standard and ISO 9000 and believe that 
            both help make their organisations successful. <br>
            <br>
            <b>Is the IiP Standard a voluntary option in UK? Are there any 
            bodies or organisations or sectors that compel members to adopt the 
            standard?</b><br>
            Yes, the Investors in People Standard is entirely voluntary in the 
            UK. Many organisations and trade bodies encourage their members to 
            work towards the Standard as they realise the benefits it can bring. 
            However, there are no sectors that make achieving the Standard 
            compulsory. Despite that, we now have over 32,000 recognised 
            organisations here in the UK. <br>
            <br>
            <b>Are there any companies at a disadvantage because they do not 
            have the IiP Standard? How would having the IiP Standard confer 
            advantage on a company bidding for a tender?</b><br>
            Many organisations tell us that being recognised as Investors in 
            People has helped them generate new business because customers 
            recognise that organisations that are recognised will provide 
            quality customer service delivered by highly motivated employees.<br>
            <br>
            <b>One criticism of the IiP is that the follow-up procedures for 
            evaluation of training produce a lot of paperwork and are 
            time-consuming. As a consequence, some companies and organisations 
            are thinking hard about whether to renew their IiP status. What are 
            your views? </b><br>
            <br>
            We are very proud of our retention rate, which currently stands at 
            90 per cent. As you can see, the massive majority of organisations 
            that achieve the Standard find that its benefits more than 
            compensate for the time and money involved in achieving it. However, 
            we are always looking for ways to improve the Standard. Streamlining 
            the assessment process to reduce the time and money involved is just 
            one. <br>
            The Standard itself went through a review in 2000 and following that 
            process, it is results-driven and not process-driven. This has 
            helped to reduce the amount of paperwork involved in the Standard. 
            Assessments are now based almost entirely on interviews rather than 
            piles of documents. <br>
            <br>
            <b>Should IiP UK relinquish the international work and license it 
            out to another party because its efforts are not tangential to its 
            core business? </b><br>
            The international development of the Standard was not undertaken as 
            a commercial activity in itself although, of course, we have tried 
            to operate it in such a way as to make a contribution to Investors 
            in People as a whole. <br>
            However, the international development has a wider agenda than just 
            driving a profit. It is about increasing the value of the Standard 
            to organisations by ensuring it is recognised as the international 
            people management standard, enabling Investor in People 
            organisations to signal their excellence to new markets, suppliers, 
            clients etc. across national and international boundaries. It is 
            also of considerable value to multinational organisations and 
            enables them to implement the Standard across their operations 
            rather than just in the UK. <br>
            Given all this, international development has to operate in such a 
            way that maximises the impact of our activities without compromising 
            the quality of the Standard. This is expensive, both for us and for 
            our overseas partners. <br>
            In relation to the comment about it being tangential to the core 
            business, a report recognised the importance of the international 
            work, but concluded that this was not a suitable activity for 
            Investors in People UK as a Non-Departmental Government Body (NDGB). 
            It did not question the validity or desirability of this work but 
            instead recommended that it be contracted out to a third-party 
            organisation, able to commit more resources to it and better geared 
            towards running a growing, commercial activity. However, Investors 
            in People UK will still be responsible for overall international 
            strategic management and quality assurance. All international 
            licence agreements will still be signed by Investors in People UK as 
            the organisation responsible for the intellectual property 
            associated with the Standard. <br>
            <br>
            <b>How would this affect the work of IiP in Oman?</b><br>
            It shouldn't have anything other than a positive impact. The project 
            in Oman is already fairly well established and we are supporting our 
            key contact out there in encouraging the Omani government to develop 
            a long-term project under licence to Investors in People UK. Any 
            agreements in Oman, or elsewhere, will be honoured by the incoming 
            international contract management organisation. If anything, there 
            will be improved service and support available for overseas 
            partners. I fully expect the project in Oman to go from strength to 
            strength. <br>
            <br>
            <b>Would it be likely that an IiP office would be established in 
            Muscat? </b><br>
            Yes, it is likely that an office would be established in Muscat. <br>
            <br>
            <b>Many companies in Oman have been going through ISO accreditation, 
            and are sceptical about applying for IiP because it appears to be a 
            repetitive exercise. How does the IiP Standard enhance an Omani 
            company's ability to conduct its business more effectively?</b><br>
            Whilst Investors in People has some similarities with ISO, it is 
            focused on different areas within the organisation. Whereas ISO is 
            concerned with processes, Investors in People is concerned with 
            outcomes. As such, it focuses on improving the performance of people 
            in the organisation in order to improve the performance of the 
            organisation as a whole. So whilst it is about people development, 
            in effect it is a total business development tool. A significant 
            number of organisations in Oman have already seen the benefits of 
            working with Investors in People, as have over 32,000 organisations 
            worldwide. <br>
            <br>
            <b>There is a huge push for much needed Omanisation of jobs in the 
            Sultanate. If expatriates and Omanis were not treated equally in an 
            organisation, would this disqualify a company from reaching IiP 
            status? </b><br>
            Regarding training and development, Investors in People ensures that 
            everyone within an organisation has the same equality of opportunity 
            to access training and development. However, it is not intended to 
            override or undermine the employment legislation within an 
            individual country and the Standard has to be interpreted in 
            relation to the specific local circumstances. As a number of 
            organisations in Oman have already been successfully recognised as 
            Investors in People, this is not an insurmountable issue in Oman. We 
            are dealing with similar circumstances in other Middle Eastern 
            countries and, in particular, South Africa. This is one of the main 
            reasons for developing a local infrastructure that can take account 
