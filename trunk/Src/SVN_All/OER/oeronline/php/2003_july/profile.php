                    <b>As thoughts turn to jetting away for the summer, Nayl 
                    D'Souza chats with Abdul Rahman Al Busaidy, chief executive, 
                    Oman Aviation Services, about which direction the company is 
                    heading and which routes his own career has taken </b><br>
