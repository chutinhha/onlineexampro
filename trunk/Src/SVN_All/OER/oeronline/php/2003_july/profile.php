                    <img border="0" src="profile.jpg" align="left" hspace="5" vspace="5" width="150" height="195">You perhaps should think of Oman Aviation Service Co. (OAS) 
                    SAOG as two separate units, advises Abdul Rahman Harith Al 
                    Busaidy, chief executive of OAS, the Sultanate's fifth 
                    largest public company in terms of total revenue achieving 
                    an income of RO60.02 million in 2002. There's Oman Air, the 
                    spearhead of the business. And then there's the airport 
                    services division which incorporates cargo handling and 
                    passenger support. Passenger support incorporates the 
                    catering services unit OAS owns and operates which dished 
                    out 2.1 million meals for hungry travellers last year as 
                    well as the duty free business at Seeb and Salalah airports 
                    in Oman which it jointly operates with Ireland's Aer Rianta.<br>
                    Undoubtedly, many people forget that OAS is more than an 
                    airline but because Oman Air's chartered international and 
                    domestic flights were responsible for 66 per cent of the 
                    company's revenue (excluding the RO6.4 million it receives 
                    from air charter services which falls into a separate 
                    revenue stream), one could be forgiven for making this 
                    observation.<br>
                    Few would envy Al Busaidy's job. The aviation industry is 
                    still reeling from depressed demand (following 9/11, the 
                    Iraq war and the SARS epidemic), sky-high insurance 
                    premiums, high fuel charges and declining air ticket prices. 
                    The airline will need to press ahead with many ambitious 
                    strategies to guarantee further progress.<br>
                    &quot;One of the key concerns for any airline today is continued 
                    erosion of passenger yields with each airline trying to 
                    under price the competition to protect and increase its own 
                    market share,&quot; he explains. <br>
                    Appointed three years ago as the general manager of Oman 
                    Air, Al Busaidy has made an immediate impression to manage 
                    passenger yields across the airline's network. Moreover, he 
                    has helped steer the company on a growth trajectory by 
                    forging ahead with a cost rationalisation programme, an 
                    aggressive expansion of its network, measures to improve 
                    staff productivity, greater attention to customer service, 
                    and the implementation of innovative promotions to assist 
                    inbound tourism into the Sultanate. Indeed, his efforts to 
                    stage a swift recovery quickly earned him a deserved 
                    promotion. <br>
                    Al Busaidy, 45, exudes confidence and charm. He is 
                    compassionate, softly spoken and receptive. &quot;People tell me 
                    I'm a good listener, that I'm fair and that I'm a good 
                    motivator,&quot; he says. He is commended a great deal by his 
                    staff for regularly putting pen to paper to thank individual 
                    employees when it is deserved. But by his own admission, he 
                    is not a soft touch. &quot;People misinterpret kindness as a 
                    weakness. If I'm provoked, my staff will find a tiger 
                    beneath my calm exterior.&quot; Fortunately, he reassures me that 
                    this is something few have had the misfortune to discover.
                    <br>
                    The turnaround for OAS may already be in full swing with a 
                    reduction in net loss in 2002 of 33 per cent compared to the 
                    net loss the company posted in 2001 in addition to growth in 
                    capacity of 23 per cent and an increase in passenger traffic 
                    of 16 per cent. Nevertheless, the company's chief executive 
                    is confident this could happen by the end of 2003. The 
                    cautious optimism stems from the maturity of Oman Air's new 
                    routes and the expected increase in flight movement through 
                    Seeb International Airport. Fuel prices have also returned 
                    to pre-Iraq war levels. <br>
                    &quot;Oman Air started the year positively with traffic 
                    increasing 35 per cent by February-end compared to the first 
                    two months of last year. In March, the growth ratio dropped 
                    to five per cent. Meanwhile, fuel prices shot up 37 per cent 
                    in the first three months putting an extra cost burden of 
                    almost RO500,000 on the airline,&quot; Al Busaidy reveals. <br>
                    Oman Air is operating additional services to Cairo, Zanzibar 
                    and Dar Es Salaam this summer as well as a higher frequency 
                    of flights to Salalah and Dubai. The airline is also 
                    pursuing additional traffic rights to some countries and has 
                    tactically partnered Gulf Air on a number of code-share 
                    flights to increase its network utility. <br>
                    <br>
                    <b>Chosen career path </b><br>
                    Ironically, if Al Busaidy hadn't have gone into aviation 
                    management over 20 years ago, he would probably have pursued 
                    a career in fixing planes. <br>
                    In 1977, &quot;blessed as an A-grade student&quot; to use his own 
                    words, he left school and chose to study a degree in 
                    aviation management at Florida University in the US. 
                    However, initially the education advisors had suggested he 
                    study aeronautical engineering. This was something he 
                    seriously considered until a niggling worry kept him from 
                    committing to the course. He was convinced such a discipline 
                    wouldn't be in great need in Oman upon his return so he 
                    dropped the idea of embarking on this degree. &quot;Perhaps the 
                    upshot of choosing aeronautical engineering would have been 
                    for me to move into aircraft maintenance,&quot; he muses. <br>
                    <br>
                    <b>Frequent flyer </b><br>
                    Believe it or not, another irony is that the chief executive 
                    of Oman Aviation actually doesn't enjoy flying. &quot;I used to 
                    get excited about flying but the novelty has worn off. In 
                    fact, I find it torture on long haul flights,&quot; he concedes. 
                    This said, Al Busaidy actually clocks up more miles on 
                    flights in a year than many people are likely to travel in a 
                    lifetime. For business, he usually takes two flights to the 
                    Far East, five to India and 24 within the Middle East. For 
                    leisure, Al Busaidy, a devoted husband and father of four, 
                    travels with his wife to visit his in-laws in the US and 
                    also makes two visits to see his children who are studying 
                    in Australia. <br>
                    <br>
                    <b>Mystery guest </b><br>
                    Al Busaidy says he aims to make more visits to frontline 
                    operations at OAS in the future, particularly various 
                    divisions like maintenance. &quot;I make a point of visiting each 
                    department of the company at least once but maybe I should 
                    do it more often,&quot; he admits. Despite this, he takes the 
                    opportunity of secretly surveying the company when 
                    travelling on Oman Air. As he doesn't request a special 
                    meet-and-greet service, many staff are unaware that their 
                    boss is at the airport or on board their plane. &quot;This allows 
                    me to cast a critical eye on the company's operations as if 
                    I were an everyday customer. From my observations, I usually 
                    make suggestions to supervisory staff upon my return.&quot; <br>
                    To remain competitive airlines must of course continuously 
                    upgrade their service and give added value to our customers. 
                    Al Busaidy describes an effective example where improvements 
                    have been made since February 2003. &quot;One of our weak areas 
                    was something simple like answering the phone,&quot; he says. 
                    &quot;Since the introduction of a new training regime and a new 
                    customer call management system, lost calls have dropped to 
                    seven per cent from 44 per cent at our main call centre.&quot;
                    <br>
                    <br>
                    <b>Flying high</b><br>
                    Al Busaidy prides himself on the fact that he started 20 
                    years ago on the lowest rung of the aviation management 
                    ladder. Indeed, he worked his way up from a trainee to an 
                    executive manager within seven years. Nowadays, OAS clearly 
                    looks at training as an investment rather than a cost and it 
                    runs a graduate training programme for at least 10 Omanis 
                    every year to be given the skills and know-how to become its 
                    future leaders. &quot;I have personally tracked a variety of 
                    staff being trained within the company and I'm very proud 
                    that one of my colleagues who I helped to train is now 
                    working as a chief executive in Qatar.&quot; <br>
                    The Omanisation programme at OAS has certainly helped it to 
                    focus on quality as well as quantity. Having said this, the 
                    number of Omanis in the company grew to 1,768 at the end of 
                    2002, representing 75 per cent of staff. <br>
                    <br>
                    <b>Future prospects</b> <br>
                    OAS is currently preparing to table its shareholders to 
                    raise the company's share capital from RO12 million to RO50 
                    million. It is hoped that if the government (the company's 
                    shareholder with a stake of 34 per cent) accepts the 
                    proposal, other financial institutions would also be 
                    encouraged to participate. The company's articles of 
                    association are also expected to change soon to allow a 
                    strategic investor from abroad to acquire a 25 per cent 
                    stake in the company. <br>
                    Meanwhile, Al Busaidy, in the past few months, has been 
                    working on his thesis for an MBA at University of 
                    Strathclyde. How does he possibly manage to find time to do his job successfully, study an MBA and enjoy 
                    fishing and swimming? I guess this has much to do with his 
                    personal on-time performance that must be as high as that of 
                    the airline which he heads. OAS is in safe hands!</font></p>
