            <font face="Verdana"><font size="4" color="#FF0000">Trade on a 
            raised pitch<br>
            <br>
            </font>
            <font size="1"><b>It's a boom time for the local bourse, thanks to 
            cheap share prices and persistent demand from institutions and 
            investors. Such a positive sentiment is likely to continue, reports 
            OER </b><br>
            <br>
            With the local bourse witnessing a boom time, investors and market 
            operators have heaved a sigh of relief. Muscat Securities Market (MSM) 
            General Index - the barometer of the market - had touched a record 
            high of 239.9 points in mid-June, while a large number of shares 
            peaked to a 52-week high following a persistent investment demand 
            from institutions and investors alike. <br>
            Both market pundits and investors believe that the positive note 
            that gained pitch towards the end of last year is likely to 
            continue, but at a slower pace. The genuine investment demand is 
            apparent in the increase of trading volumes on the bourse in the 
            last four months. <br>
            Renewed interest of institutional investors from neighbouring 
            countries, a declining interest rate and better performance of the 
            corporate sector are some of the contributing factors. <br>
            &quot;The improved performance of the corporate sector, coupled with 
            ample liquidity in the financial system, helped the bullish trend on 
            the bourse. Also, the economy is fine and the confidence of 
            investors in the market has been improving,&quot; says Abdullah Al Salmi, 
            Executive Vice-President, CMA. &quot;The sentiment is better in the whole 
            region,&quot; he adds.<br>
            The bullish trend is evident from the 25 per cent growth in MSM 
            General Index - from 191.9 points in end-December 2002 to 239.9 in 
            mid-June 2003. An increase of more than 26 per cent over the last 
            year. <br>
            &quot;There was a significant improvement in the institutional 
            participation last year as compared to the previous year. The entry 
            of foreign players who were attracted by a comparatively low 
            valuation of stocks also contributed to the positive sentiment on 
            the local bourse. Omani stocks are cheaper than those in many other 
            Gulf markets in terms of price earning (P/E) ratio. Prices, however, 
            may vary on the basis of sectors,&quot; says Chinku Thankappan, research 
            analyst with United Securities. The average P/E ratio at 12.5 by the 
            second week of June on MSM is still attractive when compared to 
            other regional markets, such as Kuwait or Saudi Arabia. <br>
            For Saudi Arabia it was 30.1, Kuwait 27, Qatar 18.6, the UAE 13.6 
            and Bahrain 13.3 during the same period. The Kuwaiti bourse hit a 
            record high not only in terms of share value, but traded volumes as 
            well. Market pundits were surprised at the boom at the time of war.<br>
            Institutional and corporate investors of neighbouring countries have 
            now realised that it is a good opportunity to invest in the Omani 
            bourse, as share prices are relatively cheap. &quot;The presence of 
            institutional investors,&quot; says Thankappan, &quot;had improved the 
            sentiment. This, in turn, helped retail investors stage a comeback. 
            High networth individuals in the GCC have started bringing back 
            their portfolio investments from other countries.&quot; <br>
            Investments are flowing from Kuwait and the UAE as well, he adds. Of 
            course, a major factor that helped the rally was an improvement in 
            the overall profitability of the corporate sector last year, as well 
            as in Q1 this year. &quot;This prompted the upward trend in the market,&quot; 
            notes Thankappan. The dividend payment by the corporate sector last 
            year was better than that of the previous year, which attracted the 
            attention of investors. <br>
            A steady rise in oil prices and the subsequent government spending 
            also influenced the market sentiment to a certain extent. In fact, 
            better earnings from crude compensated the decline in the oil 
            production in the recent past.<br>
            Among different sub-sectors, banking was an out-performer. It could 
            be due to the combined profit of commercial banks that showed good 
            growth to touch RO59.8 million last year. There was, however, an 8.2 
            per cent decline in the combined profit of banks during Q1 this year 
            as compared to the corresponding period last year. &quot;There may be a 
            decline in banks' earnings in Q2 of the current year,&quot; says Fahad Al 
            Khalili, Investment Manager of Al Madina Financial and Investment 
            Services. <br>
            The declining trend in the interest rate, coupled with excess 
            liquidity in the financial system, also encouraged the investor. 
            &quot;There is ample liquidity in the financial system now. Pension 
            funds, high net worth individuals and other retail investors have 
            enough liquid cash,&quot; says Khalili. Market operators expect an 
            over-subscription to BankMuscat's RO25 million bond, he says. 
            &quot;Investors are happy if they can get a seven per cent yield per 
            annum.&quot;<br>
            Stocks that outperformed on the bourse last month include Flexible 
            Industrial Packages, Renaissance Services, National Aluminium 
            Products, Al Amal Industries and Al Batinah Development. Banking 
            stocks were in demand recently and the sectoral index outperformed 
            the MSM General Index. <br>
            Although the upward movement was widespread, even across the border, 
            few stocks lost ground. Most of those that declined belonged to the 
            non-pivotal category with many of them at the liquidation stage or 
            awaiting restructuring. These include Gulf Mushroom Products, 
            National Rice Mills, Omani European Food, Oman Filters Industry, 
            Oman Education &amp; Training and Investment, Gulf International 
            Chemicals and Oman National Livestock Development. <br>
            Apart from the rise in share prices, traded volumes on the market 
            witnessed phenomenal growth in the last five or six months. &quot;The 
            average of traded volumes on the market has moved up to RO2.2 to 
            RO2.5 million per day from RO1 million a day earlier,&quot; adds Al 
            Khalili. The growth in the volume is a contribution by institutional 
            investors, especially pension funds, which is a healthy trend.<br>
            The bullish trend in early 1998 was mainly brought in by retail 
            investors, who made speculative investments without considering 
            fundamentals of companies. Contrary to the trend in previous years, 
            investors are enthusiastic even in summer this time. This has 
            resulted in a marked improvement in volumes. Generally, the bourse 
            witness listless activity in summer as more people travel abroad.
            <br>
            The series of reforms introduced by market watchdog Capital Markets 
            Authority (CMA) had a positive impact on the bourse. The 
            government's move to divest stake in state-controlled firms is 
            likely to augment the depth, as well as liquidity in the market. 
            &quot;Even now, Oman Cement and Oman Flour mills are actively traded 
            stocks on the bourse. <br>
            The divestment will further enhance liquidity in the market,&quot; 
            Thankappan says. The government has identified four state-owned or 
            semi-government firms for divestment. These are Oman Cement Company 
            (OCC), Al Maha Petroleum Marketing Company, Oman Telecommunications 
            Company (Omantel) and Oman Flour Mills. The authorities have decided 
            to sell 12.5 per cent stake in OCC, 65 per cent holding in Al Maha 
            Petroleum, 20 per cent stake in Omantel and eight or nine per cent 
            holding in Oman Flour Mills. All these will help attract 
            institutional investors to the market. <br>
            A booster to the trading volume on the market was the listing of 
            bonds by leading institutions. BankMuscat and United Finance Company 
            came out with attractive bonds recently. Al Omaniya Financial 
            Services is also planning to come out with a bond issue to meet the 
            company's need for long-term funds. The secondary market for debt 
            instruments is expected to get a boost with the listing of 
            BankMuscat's recently floated bond, aggregating RO25 million. <br>
            According to Thankappan, the primary factor that is likely to drive 
            the market in the coming days will be Q2 results of the corporate 
            sector. &quot;The trend is expected to continue, but the pace of growth 
            may not be the same,&quot; he says, adding: &quot;Analysts were expecting a 
            reaction in June, but the bourse did not witness any correction so 
            far.&quot;<br>
            Taking a different stand, Khalili says that the market is likely to 
            face a slight correction. &quot;But the long-term trend is up and the 
            index may move up by another 10 to 15 per cent before the yearend.&quot;
            <br>
            The market is likely to sustain at this level for some more time and 
            a further upward movement is unlikely, feels an investment manager 
            of a bank. &quot;There are also chances of a normal correction,&quot; he adds.</font></font></p>
