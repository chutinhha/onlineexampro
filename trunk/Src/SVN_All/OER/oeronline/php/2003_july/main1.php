            <font face="Verdana" size="1"><b>Based on the coveted rankings of 
            Oman's largest corporates, the OER 20, we rank the top manufacturing 
            companies in Oman by revenue </b><br>
            <br>
            The OER Top Factories in Oman ranking is a spin-off from the 
            KPMG-verified OER 20 (See Oman Economic Review, May 2003 issue), and 
            uses the same data, criteria and parameters for evaluating the 
            companies.<br>
            The year 2002 will go down in the history of Oman's manufacturing 
            sector as the year of consolidation. It wasn't a bad year for the 
            economy to begin with. And definitely not for the manufacturing 
            sector, with six factories in Oman falling within the OER 20 
            registering a combined increase of RO13.10 million in revenues - a 
            growth of 12.03 per cent as compared to 5.3 per cent averaged by the 
            OER 20 companies. <br>
            <br>
            <b>Sectoral trends </b><br>
            The year 2002 saw several fiscal and economic developments in the 
            local and external spheres. In fact, the Industry Sector Index was 
            one of the biggest drivers of the general MSM (Muscat Securities 
            Market) Index. <br>
            The Industry Sector Index started 2002 at 116.38 points and closed 
            the year at 154.28 points, posting a growth of 32.57 per cent as 
            compared to the General Index's 26.16 per cent and the Service 
            sector Index's 2.42 per cent growth. The only index to have 
            outperformed the Industry Sector Index was the (heavyweight) Banking 
            &amp; Investment Sector Index, with 36.67 per cent growth. <br>
            <br>
            <b>Gainers and losers </b><br>
            There was no new entrant into the OER 20 and, as an upshot of that, 
            none in the OER Top Factories either. Overall, however, there was 
            movement within the contenders. Oman Flour Mills (OFM) dropped two 
            notches in the OER 20 (from rank 10 in 2001 to rank 12 in 2002), 
            Oman Cement Company (OCC) climbed a step (rank 14 to rank 13) 
            despite a negative 0.55 per cent growth in revenues while Oman 
            Cables Industry (OCI) leapt up a couple of blocks (rank 17 to rank 
            15), registering an impressive 25 per cent jump in revenues. <br>
            The largest gainer, however, was Oman National Electric, clocking a 
            whopping 45 per cent jump in profits and a corresponding two-rank 
            jump (from rank 20 to rank 18). Among the others, Oman Refreshment 
            Company (rank 15 to rank 16) and Areej Vegetable Oils (rank 19 to 
            rank 20) both slipped a rank each. <br>
            OFM retained its numero uno slot as the Sultanate's leading 
            manufacturing unit. The company recorded a 3.20 per cent growth 
            during 2002. OFM's acting general manager Ian Watson says that his 
            company has looked at every aspect of its operations, and pays 
            emphasis on maintaining quality standards among its staff as well as 
            its products to deliver customer satisfaction. &quot;In slimming down 
            operations and making them efficient, we have looked at the 
            workforce, our procedures, as well as the flexibility and depth of 
            our marketing resources,&quot; he says. <br>
            OCC come out tops on the profits parameter, and how. In a market 
            where margins were constantly under pressure throughout the year, 
            OCC managed an impressive 50 per cent growth rate year-on-year. 
            &quot;There is stiff competition in the cement industry in Oman and the 
            UAE. We are successfully meeting this with a well-structured pricing 
            policy. The company is constantly taking measures to upgrade its 
            facilities to serve its customers better,&quot; says Qahtan bin Yarub Al-Busaidi, 
            Chairman of OCC. <br>
            <br>
            <b>Industry standards </b><br>
            The Top-3 OER factories have been awarded the prestigious His 
            Majesty's Cup for Excellence on numerous occasions. Consider this: 
            OCC has won the Cup for a staggering 10 times whereas OFC and OCI 
            have bagged it eight and four times, respectively. In fact, OCC and 
            OFC were excluded from participating in HM's Cup 2002 because of a 
            change in the contest rules that disallows factories that had won 
            the Cup for more than five times from participating.<br>
            The evaluation criteria for HM's Cup have been under constant 
            review, and were upgraded recently to include a few more key 
            parameters such as volume of exports (as against the company's 
            production levels), local market share, ISO certification, 
            implementation of Health, Safety and Environment (HSE) regulations, 
            financial status etc. <br>
            The five top performers in HM's Cup 2002 were: Jotun Paints, Voltamp 
            Transformers, National Detergent Company (SAOG), Mohsin Haider 
            Darwish's (MHD) Gases Division and Oman Fiber Optic Company (SAOG). 
            Certificates of appreciation were given to Oman Cables Industry, 
            Oman National Dairy Products Company (ONDP), Voltamp Manufacturing 
            Company, National Water Heater Industry and Reem Batteries and 
            Energy Equipment Company. OCC, OFM and Raysut Cement Company walked 
            away with the awards in the category of five-time winners. <br>
            Jotun Paints Company LLC, which earlier won the Cup in 1999 and 
            2001, has been a dominant player in Oman's coating industry ever 
            since its incorporation in 1985. According to company sources, Jotun 
            had begun formulating strategies for winning the Cup as early as 
            1996. Vijay Kumar, managing director of the company, says, &quot;Our 
            domestic marketshare has been increasing steadily over the past five 
            years even though new players and imported paint products have 
            entered the market.&quot; Jotun has the distinction of being the first 
            paint company in the Middle East achieve the ISO 9001 Certification.<br>
            Voltamp Transformers Oman LLC, a 100 per cent Omani-owned, ISO 9001 
            certified company, manufactures distribution and power transformers, 
            step-up transformers, special transformers and package sub-stations. 
            For the company established in 1993, 2002 Cup is its third within a 
            decade of existence, following those won in 1998 and 2000. Sharad 
            Shah, general manager, Voltamp, says, &quot;Our success bears testimony 
            to our single-minded perseverance of quality and becoming 
            customer-centric.&quot;<br>
            Another third time winner of the Cup, National Detergent Company 
            SAOG (NDC) started manufacturing detergent powder in 1984 and later 
            commenced production of liquid detergent and toilet soap. Other 
            products in its portfolio include - disinfectant floor cleaner, 
            industrial detergents, powder bleaches and shampoos. &quot;Our products 
            are well received in Oman and GCC markets and the company enjoys a 
            45 per cent in the local market. Exports are likely to be over 70 
            per cent of the total volume,&quot; says V. Sundaresan, director and 
            general manager, NDC. <br>
            Oman Fiber Optic Company, established in 1996, has bagged the award 
            within three years of starting commercial production in 1999 after 
            winning the Flame of Excellence Award for the year 2001. &quot;We have a 
            market share of over 95 per cent in the local market and about 20 
            per cent in the GCC countries,&quot; claims Mohammed Hassan Al Theeb, 
            general manager of the company. The company was awarded ISO 9001 in 
            the very first year of commercial production.<br>
            Mohsin Haider Darwish's (MHD) Gases Division, the pioneer in the 
            manufacturing of industrial and fuel gases was established in 1972 
            and had earlier won the Cup in 1997. The company is an ISO 9002 
            accredited company since 1996. &quot;Our marketshare exceeds 80 per cent 
            in oxygen, nitrogen, carbon dioxide, dry ice and acetylene. Our 
            export volume has also substantially increased,&quot; reveals A.K. Paul, 
            general manager of the division.<br>
            <br>
            <b>Industrial hub </b><br>
            A major driving force behind industrialisation in Oman is the Public 
            Establishment for Industrial Estates (PEIE) which, in addition to 
            taking the responsibility of providing infrastructure to the 
            companies, undertakes maintenance and management of the industrial 
            estates in Oman. As a result, these estates collectively house 230 
            companies, operating in more than 400 different types of industries.
            <br>
            The PEIE has industrial estates in Rusayl in Muscat, Sohar in 
            Batinah region, Raysut in Dhofar, Nizwa in Dhakhiliya, Buraimi in 
            Dahira and Sur in the Sharqiya region. Says Ali bin Masoud Al 
            Sunaidi, Undersecretary, Ministry of Commerce and Industry, &quot;Along 
            with the government's policies of loans and exemptions, these 
            estates have been one of the major reasons for industrial growth in 
            Oman.&quot; <br>
            The 230 factories in all the estates accommodate more than 15,000 
            workers, including Omanis and expatriates. The total investment of 
            all these factories add up to RO250 million. The upcoming Sohar 
            estates is further expected to spur manufacturing activities.<br>
            Among the individual estates, Rusayl Industrial Estate (RIE) is the 
            largest in terms of the number of industrial units. Established in 
            1985, it has a strategic location and is the most productive estates 
            of all. The top factories like Oman Cables, Oman Cement Company and 
            Oman Fiber Optics all underwent major expansions in the last seven 
            to 10 years. This adds to the success of the industrial estate. 
            Around 98 per cent of RIE is already occupied and the other two per 
            cent is reserved. <br>
            <br>
            <b>Sick factories </b><br>
            Dr Younis, professor of commerce and economics at Sultan Qaboos 
            University, explains that the real problems being encountered in 
            many manufacturing businesses are usually related to the way they 
            are managed in most areas of the business operations. They must face 
            the challenge of the marketplace in terms of product quality, 
            delivery schedules, customer service, and compete on all measures of 
            productivity and profitability. This is what would make a successful 
            business, and it has been the case all around the world. For 
            example, when a business does not demonstrate a responsibility to 
            pay back loans, especially when the bank obtain a guarantee from the 
            government, and then there will be a lack of motivation to excel in 
            generating wealth. He says, &quot;It is, of course, good to encourage 
            entrepreneurial spirit. However, the government should not always be 
            held accountable at the expense of creating real development of the 
            local industry. By acting as a guarantor on loans for new factory 
            start-ups, the banks have less reason to take under performing firms 
            to task and few seem to spend time to examine why such factories are 
            failing.&quot; <br>
            Another worry is the number of businessmen that are able to 
            bamboozle others to lend them more money even when their business is 
            ailing and they are taking a back seat in their company's affairs. 
            Propping such companies up is obviously not such a good idea. &quot;The 
            issue of business ethics is important and must be inculcated within 
            the business community. This does not happen overnight, but the 
            presence of adequate controls through market dynamics would instil a 
            sense of accountability and good business practices.&quot; <br>
            If there is not enough motive to succeed and overcome adversity, 
            businesses that are encountering problems will probably not achieve 
            better results. &quot;Those factories who default on their loans need to 
            have their debts recovered quickly and efficiently, and a chance to 
            a better management or ownership is given.&quot;<br>
            <br>
            <b>Turnaround tactics</b><br>
            Turnaround professionals are currently in great demand worldwide. 
            This is primarily due to the global recession that has been seen 
            over the past few years which has put many healthy companies into 
            trouble. New corporate governance laws and regulations have also 
            increased the need for turnaround management as these laws have 
            placed the onus and the responsibility on the board to directors to 
            act in the best interests of the company and its shareholders. <br>
            It is imperative that the company hires the right turnaround 
            professional to do the job. &quot;Turnaround professionals are hired 
            either by the Board if they act quickly or by the lenders or finally 
            by a receiver appointed by the Court,&quot; says Mukhtar Hasan, director 
            of Renaissance Services and Gulf Investment Services. <br>
            After identifying the major problems, the turnaround professional 
            develops a strategic plan with specific goals and detailed 
            functional actions, approved by all key parties in the company. 
            Presenting the plan to key parties outside the company - bankers, 
            major creditors and vendors - should regain their confidence. <br>
            The next step is to put into place the top management team who will 
            lead the turnaround effort. Then, swift cost cuts in line with the 
            action plan need to be taken to ensure that the bleeding can be 
            stopped. Once it has, turnaround efforts can be directed towards 
            making current operations effective and efficient. <br>
            Finally, the company slowly returns to profitability. While earlier 
            steps concentrated on correcting problems, this one focuses on 
            institutionalising an emphasis on profitability and return on 
            equity. Financially, the emphasis shifts from cash flow concerns to 
            maintaining a strong balance-sheet, long-term financing, and 
            strategic accounting and control systems.</font></p>
