            Attiyah is well aware of the impact Iraqi production might have on 
            OPEC's quota system, and on prices in general. &quot;Iraq will come to 
            the market. That's their right. We have to help them and we also 
            have to make room for them.&quot; As such, OPEC economists are busy 
            crunching numbers in their economic models in Vienna. &quot;We should not 
            wait until the crisis comes,&quot; says Al Attiyah. (Iraq is a member of 
            OPEC, but has been outside the cartel's quota system since the 1991 
            Gulf War). <br>
            <br>
            <b>What next? </b><br>
            Against this conflicting backdrop, what is the likely outcome? 
            Chalabi may have a point when he points to weak supply/demand 
            fundamentals in years to come, but it would be wrong to dismiss 
            OPEC's ability to manage oil prices just yet. The cartel has been 
            written off before, but has a habit of bouncing back. In late 2001, 
            prices dipped to around US$17 per barrel, plunging OPEC members into 
            economic crisis. When push came to shove, OPEC used bully-boy 
            tactics to get non-OPEC rivals such as Russian, Mexico and Norway to 
            rein in output in a bid to support prices. <br>
            At the time, OPEC threatened to flood the market with cheap oil to 
            drive the price down to US$5 per barrel. The move would have hurt 
            OPEC members hard, but Gulf oil costs just US$1 per barrel to pump, 
            against US$10 or US$15 per barrel in Russia and the North Sea. 
            Ultimately, OPEC can send virtually every other major oil producer 
            out of business at the turn of a tap, and everyone knows it. Game 
            theorists will watch events unfold with interest over the coming 
            decades - but with OPEC holding the ultimate weapon of mass economic 
            destruction, it should be able to keep prices at reasonable levels 
            for some time to come.<br>
