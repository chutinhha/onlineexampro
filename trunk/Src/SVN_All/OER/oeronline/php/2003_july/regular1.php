            <img border="0" src="oil.jpg" align="left" hspace="5" vspace="5" width="150" height="152">OPEC oil ministers gathered in Qatar last month against a healthy 
            oil price background. Fears that international oil markets would 
            plummet as soon as peace broke out in Iraq had proved unfounded: 
            West Texas Intermediate (WTI) was fetching more than US$30 per 
            barrel as the ministers descended on Doha. As such, they felt 
            comfortable leaving their production quotas unchanged - despite 
            widespread speculation that they would be forced to slash output to 
            prevent a price collapse.<br>
            &quot;It isn't a high price, it's still in the band,&quot; said OPEC President 
            Abdullah Al Attiyah, also Qatar's oil minister. OPEC has set itself 
            an unofficial target of keeping the price of the cartel's crude 
            basket between US$22 per barrel and US$28 per barrel. &quot;We're happy 
            to see it in the band, we don't want to see it over the band. We're 
            working very hard to keep prices within the band,&quot; insisted Al 
            Attiyah. <br>
            <br>
            <b>Bearish outlook </b><br>
            However, the rosy short-term picture cannot disguise the more 
            bearish outlook further ahead. With Iraqi crude soon to come back on 
            stream - and the oil demand growth stunted by the global economy - 
            the medium-term prospects for prices are weak. The prospects for the 
            coming two decades are, if anything, worse still.<br>
            Dr Fadhil Chalabi, president of the Center for Global Energy Studies 
            in London, is one of the leading bears. Speaking in Dubai in 
            mid-June, he told delegates at a Massachusetts Institute of 
            Technology conference that fundamental supply and demand factors 
            strongly point to a weakening oil price environment. <br>
            On the supply side, he highlights Iraq. &quot;Iraq is a giant producer 
            that was kept dormant. Until 9/11, Iraqi production was not that 
            important: Saudi Arabia was the world's major oil producer, and the 
            United States felt confident about security of supply. Now that has 
            changed.&quot; <br>
            Chalabi says Iraq could be producing eight million barrels per day - 
            the same as Saudi Arabia today - within ten years. Given that Iraq's 
            oil is cheap to find and extract, he estimates it will cost just 
            US$30 billion to bring Iraq's production up to this level (Dr 
            Chalabi was once Iraq's oil minister). That's small fry in the 
            context of oil exploration and production budgets. <br>
            <br>
            <b>The demand side </b><br>
            Even more worrying for the oil price outlook is the demand side. The 
            Energy Information Administration (EIA), part of the US Government's 
            Department of Energy, forecasts that global oil demand will rise by 
            2.2 per cent per year between now and 2020 - a rise of some 44 
            million barrels per day. (Today, oil demand is around 76 million 
            barrels per day). <br>
            Chalabi admits that if this is the case, Iraqi oil supply increases 
            carry no threat. However, he says the EIA's forecast is wildly 
            optimistic. &quot;These mathematical models have proved to be erroneous. 
            They are based on assumptions that may not be realistic. I do not 
            have much trust in these forecasts because history shows they are 
            not reliable.&quot; <br>
            By contrast, energy major Royal/Dutch Shell forecasts oil demand 
            growth of just 1.1 per cent per year over the same period - a growth 
            of 19 million barrels per day from today's level. That leaves a 
            shortfall of some 25 million barrels per day between the EIA's and 
            Shell's forecasts.<br>
            A number of factors lead Chalabi to side with the cautious analysts 
            at Shell. &quot;The Kyoto protocol will sooner or later have to be 
            reflected in consumption,&quot; says Chalabi. &quot;The ratio of oil 
            consumption/GDP has been falling steadily. Taxes on gasoline are 
            high and rising while the technology for producing hybrid cars that 
            run on hydrogen and gasoline is getting better - Japan is already 
            producing these models.&quot; <br>
            He adds: &quot;This all indicates that demand for oil will not be as high 
            as forecast. Iraqi oil in big quantities will put OPEC in a weak 
            position. The price of oil could be lower for years.&quot;<br>
