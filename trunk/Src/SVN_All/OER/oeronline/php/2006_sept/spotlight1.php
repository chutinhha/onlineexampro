</font><b><font style="font-size: 14pt" face="Verdana">Automation &amp; Cost 
Reduction Solutions</font></b><font face="Verdana" size="1"><br>
<b>Staying connected is important for the frequent flyer businessman — this 
saves time, money and resources, checks out Sarada Vishnubhatla</b><br>
<br>
</font><font face="Verdana" size="2"><b>W</b></font><font face="Verdana" size="1">ireless 
Local Area Network (LAN) support in mobile computers has not only enabled 
greater within-office network accessibility, but also opened the doors to smart 
home networking and individual connectivity for video conferencing and LAN 
interactivity.<br>
<br>
<img border="0" src="Automation.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="120" height="102">The 
end-user is likely to be provided, in future, a wider array of connectivity 
choices, with data security as the main focus area. Online meeting reduces 
travel and facility costs, eliminates the need for extra equipment as everyone 
uses own computer, allows to respond to business leads quicker and instant care 
of customers, resulting in a productivity boost. This allows organisations to 
reach customers globally expanding the market, and is fast catching up now in 
Oman too.</font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">Government 
initiatives like E-Oman help spread awareness about ICT. Ministries and business 
houses are gradually gearing up to embrace system integration. <br>
<br>
E-Oman is an initiative of Ministry of National Economy based on His Majesty’s 
vision to transform Oman into a Knowledge Society leading to a knowledge-based 
economy. The drive will trigger off setting up unified IT infrastructure, 
shareable databases while connecting the entire government via a secure 
electronic information network. <br>
<br>
The process has started. A case in point is the Ministry of Commerce &amp; Industry, 
which is embracing the new wave. Ali A Al-Muqbali, Section Head – Network &amp; 
Support, Ministry of Commerce &amp; Industry, stresses, “The point is to shift from 
paper to e-Smart Office. Multifunction machines ease our job, which can scan, 
fax and email. We are buyers of multifunction machines from Canon and we have 
reduced cost of the paper. Maintenance cost of this machine is very low. Remote 
application is possible which means, in case of any problem, the Canon executive 
comes to know about it fast.”<br>
<br>
Manmohan Singh Rehsi, General Manager, Office Automation and Supplies, MHD says, 
“Oman is open to Smart Office solutions and the competition is good. Educate 
more people about technology for maximum benefit.” <br>
<br>
R Srikanth, Divisional Manager BTT, Genetco-Canon points out, “Oman is nascent 
in technology as compared to the world but has a huge potential. It is a growing 
economy. Customers are educated and we need to understand their needs better. 
Entire offices can be networked. Any piece of information generated from a PC 
can be accessed by all.”<br>
<br>
Of course, a bank’s need for an IP solution will be different from what Ministry 
of Defence might look for. “A corporate’s need today is an IP-based 
communication system. Using Panasonic IP-based communication systems (KX TDA 
series) corporate houses can utilise the IP Telephony/IP Gateway technology to 
stay connected with their distant branch offices and enjoy unlimited intercom 
calling at no extra cost. We have implemented a complete VoIP solution with 100 
per cent IP-phones for Oman Methanol Company by linking their offices within 
Sohar, over the Fibre Optic backbone. They can also be connected with their 
overseas offices in Germany and the West Indies over the VoIP network,” responds 
Rajendra Nadkarni, Divisional Manager – Projects &amp; Engineering, OMASCO.<br>
<br>
Tajj Deen, Manager (Office Automation) of Jawad Sultan Technologies, says, 
“Services like IP Telephony are moving towards web-enabled technologies. But we 
are yet to come to a stage where repairing a plant can happen from a remote 
site.”<br>
<br>
Anil Kumar, Sales Manager, Bahwan IT, claims the group has 6-7 huge IP telephony 
projects. “Remote logging and rectifying a certain problem is very much 
possible. When consolidated information is made available to the executives from 
various departments — be it manufacturing, retail or back-end — decision making 
becomes easy,” he identifies.<br>
<br>
Devan Thuppali, General Manager, OHI Electronics highlights, “Product aesthetics 
and performance go hand in hand today. Products that deliver the desired results 
have to also blend in with the ambience of the location of the installation.”
<br>
<br>
<b>Cost Factor</b><br>
An organisation achieves desired results based on its objectives. These 
objectives require certain solutions, which result in higher productivity. But 
new technology costs hard currency. Brands and solution providers say they give 
competitive prices to integrated services offered to the customer.<br>
<br>
“Gadgets which automate help enhance products by addressing the needs and hasten 
the work flow, thus helping an organisation. Canon products are not cheap but 
competitive. We are consultants to customers offering cost-effective solutions,” 
says Srikanth of Genetco-Canon.<br>
<br>
Aesthetics play a great role in pushing a gadget to an individual. “Everyone is 
competing for the market through looks. Choice is available according to the 
needs. Customer wants the best,” says Nadkarni, OMASCO.<br>
<br>
Bahwan IT’s Kumar says, “I would like to call it investment and not cost. 
Decision-makers today treat IT as their basic need for any business. Recovering 
cost depends on the customer. Earlier the customer decides to put in IT, faster 
the returns. Your investment will not go waste. New technology will complement 
your product now.” <br>
<br>
<b>The Hotties</b><br>
Gizmos and gadgets that are web-enabled are doing all the talk today. Every 
brand name — be it an PC or cellphone — is updating the product line up, to make 
it web-friendly. Even the idiot box is no longer brainless! <br>
<br>
A universal truth is dawning on the multinational brand names the world over. 
“Brands are incidental. Solution is the driver. My solution works with all 
brands,” says Kumar of Bahwan IT.<br>
<br>
Genetco-Canon’s Srikanth points out, “Our MEAP Series 3570 Multi-Function 
machine is very popular. This comes with a remote interface and can be 
configured with all the computers on LAN. We had a bulk order of 450 machines 
from an Omani corporate. Cost-efficiency, high reliability, high speed range of 
scanners from Canon which can also do indexing, makes Ministry of Commerce &amp; 
Industry a true happy customer among so many others.” <br>
<br>
<b>Staying Savvy</b><br>
Spiralling costs can be handled in many ways. And one of them is to optimise. 
Here are a few tips from Devan Thuppali of OHI Electronics to stay savvy on the 
latest e-Smart Office market and get the best return from your investment.<br>
<br>
<b>Computers</b></font></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">These machines go 
	out of date quickly even if they are provisioned for upgradation. You can 
	always get bigger and bigger hard disk spaces but the basic cost 
	effectiveness comes in when you do not overbuy machines. It’s better to 
	lease or outsource hardware. Settle for a standard system that will be more 
	than enough for managing office software and Internet applications. </font>
	</p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Printers </font>
</b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Don’t cut corners 
	in terms of the quality of the printer. A small group of employees can share 
	a good printer on a LAN. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Try to get a 
	printer with 32 MB memory. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Think about 
	replacement toner costs before you buy. Color printers cost more to 
	maintain.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Fax Machines
</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Consider 
	functions carefully before buying, like if it enables mass faxing. </font>
	</p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Consider 
	purchasing fax software for a computer or using a web-based fax service.<br>
	Notebooks &amp; Laptops</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">A notebook or 
	laptop computer is more expensive than a desktop computer with the same 
	functionality. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Palm devices, 
	cellular phones and pagers rapidly become obsolete as new standards and 
	functions emerge. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Should be driven 
	by a clear need and cost/benefit.</font></p></li>
</ul>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Scanners</b></font></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Should be driven 
	by a clear need and cost/benefit.</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Combo gadgets 
	like a photocopier that can scan as well are profitable</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Network enabled 
	is more profitable</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Internet tools</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Technology 
	changes rapidly in the web browser area but functionality improvements are 
	slowing considerably. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">E-mail and web 
	browsers are generally compatible. Free, yet reliable software often can 
	meet your needs.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Desktop &amp; Web 
publishing</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">
	<img border="0" src="printer.jpg" width="90" height="89" align="right" vspace="2">Purchase 
	according to need. There is a wide range in the amount of money you can 
	spend in this category. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">A basic desktop 
	publishing software package can improve the quality of your internally 
	produced marketing materials. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Clear goals and 
	objectives should guide investments in web development. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Complex demands 
	in this category require dedicated staff or outsourcing.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Virus</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">The two most 
	important utilities are virus protection software and desktop maintenance 
	software. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Any additional 
	software should be driven by real needs and should pass a cost/benefit 
	ratio. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Beware of free 
	software.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">LAN</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">A local area 
	network makes life easier. Computers in the office are connected to enable 
	file-sharing, easy back-up of data, and sharing of common equipment like 
	printers and scanners. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">In general, 
	simple networks can be managed internally with the help of a how-to manual.
	</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">However, if 
	possible a LAN should be set up by qualified people who can also maintain 
	it. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Costs generally 
	include a computer to act as the server, cables, networking cards and some 
	other minor costs.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Internet</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">There is no such 
	thing as a free Internet connection. However, there are no-cost Internet 
	Service Providers. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">In an office 
	setting, speed matters. Don’t cut corners on your Internet connection.
	</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Ideally ISDN or 
	cable modem should be your first choice. Second option is a shared modem (on 
	your LAN) with a 56K speed. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">The days of 
	having one computer hooked up to the Internet are disappearing.</font></p>
	</li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Tech Maintenance</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">This function is 
	not optional and does cost real dollars. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Includes keeping 
	virus protection software current, routine desktop maintenance, regular data 
	back-up and software upgrades. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Maintenance also 
	includes the installation of new computers and possible upgrades of existing 
	hardware. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Network/data 
	security is also a maintenance function. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">This is also a 
	function that might be outsourced.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Training</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Training should 
	be ongoing and based on functional needs identified in your strategic plan.
	</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Options range 
	from how-to books to computer/web-based training to classroom instruction.
	</font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Hire according to 
	the core needs of your program. It can be expensive getting someone up to 
	speed on a complex software program.</font></p></li>
</ul>
<p align="justify" dir="ltr"><b><font face="Verdana" size="1">Administration &amp; 
Policies</font></b></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">A technology plan 
	is the foundation of your administration’s policies on technology. </font>
	</p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Policies should 
	outline appropriate use of equipment and resources. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">All equipment 
	needs to be inventoried by serial number and all equipment and software 
	should be used legally. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">All staff should 
	be aware of administrative process and policies. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">A staff person 
	needs to manage technology policies and plans for the agency.</font></p>
	</li>
</ul>
<p align="justify" dir="ltr"><font face="Verdana" size="1">A well-developed 
technology plan will:</font></p>
<ul dir="ltr">
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Help you 
	prioritise and focus efforts to reach your goals. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Involve key 
	people in the conversation and attract their support. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Help you budget 
	and make appropriate, cost-effective purchases. </font></p></li>
	<li dir="ltr">
	<p align="justify" dir="ltr"><font face="Verdana" size="1">Serve as a key 
	tool in pursuing technology funding.</font></p></li>
</ul>
