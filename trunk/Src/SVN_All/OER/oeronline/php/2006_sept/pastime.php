</b><font face="Verdana" style="font-size: 14pt"><b>Family Comes First</b></font><font face="Verdana" size="1"><b><br>
Amer Mustafa Al-Fadhil, Vice President, External Affairs — The Wave, chats with 
Uma Devi Jadhav on the simple basic joys of life</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt; font-weight: 700">
<img border="0" src="past.jpg" width="150" height="218" align="left" hspace="5" vspace="2">I</font><font face="Verdana" size="1">f 
you notice how Amer orders his coffee decaf — double shot, skimmed milk, no foam 
and less vanilla — you realise he’s a food and health buff. And as he chats 
about his leisure time — whether it is helping his children with homework, 
maintaining a certain fitness level at the gym, spending time with his wife and 
children, or travelling — one quickly learns that a lot of thought has gone into 
why he does what he does and how!<br>
<br>
“Generally a very domestic person,” that’s how Amer wants to qualify his 
pastime. Leisure time, for the workaholic VP, is “with my family — my immediate 
family (wife and two children) and also my extended family.” Getting back from 
work — that’s around 5 pm — Amer likes to spend “meaningful time with my 
children and then go to the mosque for evening prayers.” An early dinner, and 
then helping the children with their schoolwork. “I also read to them before 
tucking them into bed around 8 pm,” confides Amer.<br>
<br>
The core values of a true family man comes out. “By 9 o’clock it’s time to be 
with my wife. It’s so important to keep time aside for ourselves and find out 
how the day has been for both of us. I cherish this time with my wife daily,” he 
adds. (I do hope my hubby and all the men from the corporate world are reading 
this!)<br>
<br>
Evenings are strictly devoted to the family. So, the fitness regimen three times 
a week at the gym is either early morning before work, or mostly through 
lunchtime for about an hour and half. “Business days, I don’t have much time. So 
I’m fine with just 5 to 6 hours of exercise a week.” Diet is high protein low 
carbohydrate. <br>
<br>
How about weekends? “I go for a walk on the beach all by myself. I find it 
extremely soothing. It gives me my weekly dose of peace and tranquility. It’s 
absolutely therapeutic,” explains Amer.<br>
<br>
Taking a break once a year and travelling is his way of unwinding during a 
well-earned break. “I’m a different person when I’m travelling with both, family 
and friends. I’m caring, responsible and organised for the family. But with my 
friends, I just chill and let them lead. Spending time with the family and 
travelling are few of the joys in life I look forward to.” <br>
<br>
Discovering who you are...that’s the basic philosophy for Amer, who’s 
instinctively a very religious person. “We are all here for a reason, and we 
have to be thankful for what and where we are today. It’s therefore important 
for us to return something to the community.” So, two hours every Thursday he 
teaches his children basic religion and good manners. “I get a great sense of 
satisfaction — and when I teach, I also learn and enjoy,” Amer concludes.</font></p>
