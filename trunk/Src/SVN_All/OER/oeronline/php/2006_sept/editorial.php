    <p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">
    According to the authors, “Value innovation is a strategic move that allows 
	a company to create a blue ocean. Typically, companies in the red ocean 
	pursue incremental improvements for customers through either low cost or 
	differentiation. Value innovation helps companies make giant leaps in the 
	value provided to customers through the simultaneous pursuit of 
	differentiation and low cost. It shouldn’t be a trade off between the two; 
	exceptional value and innovation should be inseparable. Offer buyers a huge 
	leap in value, and that will give rise to new markets. That’s how you make 
	the competition irrelevant.”<br>
	<br>
	Most companies do comparative industry analysis — especially on competition 
	— to strategically plan how to keep the business growing. But the authors 
	suggest a totally different route for strategic business planning. “We offer 
	a compelling alternative. We begin by giving companies three pointers on how 
	to break out of the red and into the blue ocean. Number 1: Stop benchmarking 
	the competition. The more you benchmark your competitors, the more you tend 
	to look like them. That makes you a me-too organisation, which is the 
	opposite of what you want to achieve.<br>
	<br>
	“Second: Stop being content to swim in the red ocean. Many companies are 
	caught up in competing and don’t even look to the horizon of the blue ocean. 
	And third: Don’t count on your customers for growth. Look to non-customers; 
	they provide the most insights into how you can create new, uncontested 
	opportunities and new demand for your products or services.”<br>
	<br>
	A lot of companies have succeeded by following the Blue Ocean strategy. For 
	example, in the highly competitive airlines industry, Virgin Atlantic 
	Airways has created a Blue Ocean within a Red one. Virgin has redefined the 
	travel experience for business and first-class travellers by targeting the 
	high end of the market. The airline has proved that air travel is not just 
	about getting from one airport to another, it’s about the experience — from 
	when you leave your home to when you arrive. It includes ground 
	transportation, and options like taking a shower on arrival instead of going 
	to a hotel. <br>
	<br>
	The electronics giant LG credits Blue Ocean strategy for its 20 per cent 
	growth in turnover in the first half of this year in the Middle East and 
	Africa region. A number of other successful global companies — including 
	Nintendo, Access Distribution (Sun Microsystems’ largest value-add 
	distributor), HCL Technologies (India based IT firm), Akzo Nobel 
	(Netherlands based global paint manufacturer) — are following, and 
	benefiting, from the Blue Ocean strategy.<br>
	<br>
	It will always be important to swim successfully in the Red Ocean by 
	out-competing rivals. Red Oceans will always matter and be a fact of 
	business life. And there are arguments against the Blue Ocean. But, with 
	supply exceeding demand in more and more industries, competing for a share 
	of contracting markets, while necessary, will not be sufficient to sustain 
	high performance. Companies need to go beyond this. To seize new profits and 
	growth opportunities, they also need to create Blue Oceans.</font></td>
