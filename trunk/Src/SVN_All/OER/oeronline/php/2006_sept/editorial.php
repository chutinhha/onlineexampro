	we meet this challenge?<br>
	<br>
	Perhaps the answer lies in moving from the Red Ocean to the Blue Ocean. Last 
	year, INSEAD professors W Chan Kim and Renée Mauborgne propounded this 
	theory in their book Blue Ocean Strategy: How to Create Uncontested Market 
	Space and Make the Competition Irrelevant. Too many companies are swimming 
	in the Red Ocean of bloody competition where there is limited room for real 
	growth. The image of a vast Blue Ocean conveys the infinite possibilities 
	for profitable growth that exist with this strategy.</font><div align="center">
