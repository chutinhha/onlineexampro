		<p style="margin-left: 10px; margin-right: 10px" align="justify">
		<font face="Verdana" size="1"><br>
<img border="0" src="bahrain2.jpg" align="right" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="160" height="106">A 
		fierce debate over a proposal on property ownership rights being 
		extended to GCC citizens continues to rage — with the business community 
		opposing any limit being imposed on non-tBahraini GCC citizens. In 
		response to a popular demand, members of parliament initiated a proposal 
		to check rising property prices in the country. The final draft of the 
		proposal places two types of limits on the land that non-tBahraini GCC 
		subjects may buy in Bahrain for residential — but not investmtment — 
		purposes. First, the proposed law limits ownership of land plots to a 
		maximum of 5,000 sq mt. Second, it restricts resale of property within 
		the first five years of ownership.<br>
		<br>
		It’s very much known that the common practice of GCC owners is to buy 
		relatively large plots of lands, divide them into small pieces, and then 
		offer them for sale to the geneteral public. The practice ensures 
		maximum profit. In other words, GCC citizens are least interested in 
		small plots of lands, as it limits the profit amount that can be 
		generated. <b><br>
		<br>
		Displeasure<br>
		<br>
		</b>Clearly, the business community has shown its displeasure with the 
		proposal law, arguing that rising property prices is a global phenomenon 
		not restricted to Bahrain. It has suggtgested that rising property 
		prices reflect limited investment channels against a backdrop of 
		relatively low interest rates, together with growing availability of 
		liquidity thanks to firm oil prices. Quite a few have raised doubts 
		about the ability of the proposed law per se to eventually help 
		loweriting property prices.<br>
		<br>
		Others have questioned the second part of the proposal in particular — 
		restricting trading of the property for five years from the date of 
		purchase. They find this restriction uniquely unfair — saying that 
		investors should have the right to liquidate their assets due to 
		changing circumstances, and there can be some sort of property or 
		capital gains tax.</font></p>
		<p style="margin-left: 10px; margin-right: 10px" align="justify">
		<font face="Verdana" size="1"><b>GCC commitments<br>
		<br>
		</b>The proposed changes of property law do not, in anyway, violate 
		Bahrain’s commitment to the GCC principles. As of date, the GCC is a 
		customs union, which in turn requires adoption of a unified external 
		trade policy with non-memb-bers.<br>
		<br>
		However, the GCC is due to acquire common market status by end-t2007, 
		which in turn stipulates unrestricted freedom of factors of production. 
		Fact is that many of the other GCC governments do place restrictions on 
		property ownership.</font></p>
		<p style="margin-left: 10px; margin-right: 10px" align="justify">
		<font face="Verdana" size="1"><b>Next steps<br>
		<br>
		</b>Other than the elected parliament, the appointed Consultative 
		Council likewise endorsed the proposed law. The next steps involve 
		governmental approval of the draft, followed by issuance of a royal 
		decree. The scheme becomes law once it is published in the official 
		gazette. The authorities had rejected an earlier draft that places 
		similar restrictions on properties held for investment purposes. 
		However, the MPs agreed to confine the restrictions to residential 
		purposes in order to satisfy the government. <br>
		<br>
		It remains to be seen whether the authorities would go ahead and issue 
		the law, or delay the whole matter until after the parliamentary 
		elections. One thing for sure is that the business community is not 
		pleased with proposed changes. Fortunately, the four-year term of the 
		legislative body that originally proposed the law has now expired. Fresh 
		elections are planned during the fourth quarter of the year. Problem is 
		that the proposed law could deny Bahrain a competitive advantage it 
		presently enjoys within the GCC. While limited in resources, Bahrain has 
		traditionally relied on an ‘open door’ policy to attract investments 
		deemed esstsential to stimulate business activity.</font></p>
