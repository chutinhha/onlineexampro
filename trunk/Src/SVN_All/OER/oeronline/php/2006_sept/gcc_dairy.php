tune </font></b><font face="Verdana" size="1"><br>
<br>
</font><b><font style="font-size: 11pt" face="Verdana">C</font><font face="Verdana" size="1">ompetition 
has leveraged coverage, M&amp;As and profits, aided by pan-GCC fibre optics link, 
finds research study<br>
</font></b><font face="Verdana" size="1"><br>
<img border="0" src="upswing.jpg" align="left" hspace="5"><b>A</b>n 
upswing in the Telecom liberalisation drive has boosted mobile phone penetration 
levels in the Gulf countries. While Bahrain and the UAE, cite the Madar Research 
Study, have achieved population coverage growth figures comparable to those 
prevalent in Europe and Pacific Rim countries, the overall mobile subscription 
growth for GCC in 2005 is about 38 per cent.<br>
<br>
This sudden spurt derives from deregulation and technology expansion. All the 
GCC countries except Qatar have ended their monopoly in the telecom sector. Now 
there is more than one telecom player in the market in each country. In a 
significant move, the GCC has linked up with an inter-regional fibre optic 
network known as FARAJ (also known as FOG). This network is now connected to the 
FLAG cable system, a global fibre optic link owned and operated by FLAG Limited.
<br>
<br>
<b>Penetration Levels</b><br>
On an average, the penetration rate in the GCC is 92 per cent — with Oman and 
Saudi Arabia being the least penetrated, and Bahrain and UAE being the most 
penetrated among the Gulf countries. Bahrain tops the list with the highest 
levels of penetration at 105.8 per cent in cellular phones, followed by UAE (99 
per cent), Qatar (90 per cent), Kuwait (82 per cent), Saudi Arabia (58 per cent) 
and Oman (55 per cent) mobile penetration. (Data as of Q4-2005 according to 
Madar Research Study.)<br>
<br>
<b>UAE: </b>Etisalat’s mobile customer base has exceeded 5 million, going by the 
latest census figures. New entrant du expects to attract 10 per cent subscribers 
in the first three years of the company’s operations — and pushing for securing 
30 per cent of the overall market between 2007 and 2009.<br>
<br>
<b>Kuwait: </b>The two mobile operators in the country, Wataniya and Mobile 
Telecommunications Co. (MTC) enjoy market shares of around 40 per cent and 60 
per cent, respectively. The cellular penetration rate in Kuwait reached 77 per 
cent due to huge marketing campaigns during 2004. This translates into 2.11 
million subscribers. Fixed lines in use reached 490,000 in 2004, with a 
penetration rate of 17.82 per cent. <br>
<br>
<b>Saudi Arabia: </b>The fall in the growth of land phone networks in the 
Kingdom is mainly attributed to the fast growth of the mobile phone sector. The 
number of mobile phone users reached 13.5 million in 2005, an increase of 60 per 
cent — whereas the growth of land phone networks in Saudi Arabia was 16.3 per 
cent.<br>
Qatar: It’s still a monopolistic sector, with Qatar Telecom (Q-Tel) being the 
sole fixed and mobile telecommunications provider in the country. As of the 
Third Quarter 2005, Q-Tel had 675,400 wireless subscribers and 194,000 fixed 
line subscribers.<br>
<br>
<b>Oman: </b>Mobile penetration rate in Oman was the lowest in the GCC region. 
But things are definitely looking positive — achieving about 34 per cent in 2004 
penetration as against 23 per cent in 2003. The Omani GSM market has been 
invigorated with the entry of the second cellular operator, Nawras Telecom. By 
the end of 2005, the mobile phones market was already enjoying the fruits of a 
healthy competition. At the end of January 2006, the telecom sector in Oman 
recorded a total of 1.67 million subscribers. The primary contributor was mobile 
segment — accounting for 1.33 million subscribers. (As per August 2006 figures 
claimed by the operators, the number of mobile subscribers has crossed 1.6 
million mark.) <br>
