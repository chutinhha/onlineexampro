<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">It’s a new numbers’ game now</font></b><font face="Verdana" size="1"><b><br>
Mobile Number Portability (MNP) is here, 
finally — with Oman Mobile and Nawras scoring a joint first in the Middle East. 
Consumer remains the king — with competition veering off to a fresh concourse</b><br>
<br>
</font><b><font face="Verdana" size="2">A</font></b><font face="Verdana" size="1">fter 
a long wait, Mobile Number Portability (MNP) has become a reality in Oman from 
August 26 onwards. In the process, Oman has become the first country in the 
entire Middle East and among the select few countries in the world to have MNP. 
Number of countries including Japan, Singapore and India still don’t have MNP in 
their telecom markets.<br>
<br>
<img border="0" src="Ali_Al_Habsi.jpg" align="right" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="150" height="178">For the subscribers it means that they now have 
the option to switch over from one mobile service operator to the other and 
still retain the same mobile number by paying a nominal one time portability 
fee. This comes as a boon to some of the mobile subscribers who are forced to 
stick to their current operator despite being unhappy with its service as they 
don’t want to change their mobile number. As soon as Oman Mobile and Nawras 
announced the introduction of MNP service on August 26, handful of mobiles users 
exercised the option of making a shift. The service is open to both pre-paid as 
well as post paid customers.<br>
<br>
MNP launch in August came on the expected lines. TRA had given a decision in 
March this year on a complaint filed by Nawras against Oman Mobile on the issue 
of Oman Mobile’s delay in commencing the implementation of MNP. The decision had 
given Oman Mobile five months period to implement the MNP system. According to 
TRA, “The MNP project will encourage competition and stimulate operators 
enthusiasm to maintain their customers. In doing so, operators might introduce 
competitive tariffs or enhance the quality of service which may also include 
introduction of new services.”</font></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<img border="0" src="emad.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="120" height="158">The process of transfer involves a visit to the new operator’s customer service 
centre or a dealer to fill up the transfer application form and attach an 
identification document. The mobile users can expect to start using the existing 
number with the new service operator within 2-3 days. For the transfer, a 
porting fee of RO3 is applicable which goes to the operator from which the user 
is making an exit. Currently, the fee is passed on to the consumer but as the 
battle intensifies in the coming weeks or months, the more aggressive operator 
might start absorbing the cost to lure users from the competition. TRA gives 
that freedom to the operators as its guidelines state — “Recipient Service 
Provider (RSP) could incur the charge and exempt the subscriber at its own 
discretion.” <br>
<br>
Currently, Oman Mobile claims more than 1.2 million subscribers whereas Nawras 
pegs its subscriber base at 400,000 plus. The battle for retention and addition 
of subscribers’ means mobile users are headed for better offers in the coming 
days!</font></p>

