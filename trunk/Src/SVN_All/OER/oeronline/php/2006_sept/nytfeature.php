<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>Complimentary Genius</b></font><font face="Verdana" size="1"><br>
<b>How does a company uncover innovation opportunities? Michelin published a 
tourist guidebook much before cars became popular... and pushed a nation to get 
moving<br>
<br>
By Nicholas G. Carr </b><br>
(From Strategy + Business magazine c. 2006 Booz Allen Hamilton Inc. Distributed 
by The New York Times Syndicate)<br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
W</font><font face="Verdana" size="1">hat were Andre and Edouard Michelin 
thinking? In 1900, shortly after the two brothers took control of their family’s 
venerable rubber business in Clermont-Ferrand, France, they decided to publish a 
guidebook for tourists. Their Michelin Guide provided information on gas 
stations, hotels, restaurants and roadside attractions — along with maps and 
driving tips. The brothers printed 35,000 copies of the first edition — and gave 
them away free. The diversification turned out to be an act of genius.<br>
<br>
The brothers had already realised that they needed to shift their company’s 
focus from the production of rubber to the production of rubber products, and so 
launched a line of pneumatic bicycle tyres, and then a line of car tyres. There 
was just one problem with the latter: Automobiles were still exotic products. 
That’s where the Michelin Guide came in: A handbook for travelling by road would 
encourage the sale and use of automobiles — and in turn boost their company’s 
nascent tyre business.<br>
<br>
Tourist guides and automobile tyres are what economists today call ‘complements’ 
— products that tend to be consumed together. Think of movies and popcorn. 
Economically, complements have a symbiotic relationship. If you expand the 
supply or reduce the price of one product, demand for its complements tends to 
go up. Cut the cost of electricity, for example, and you’ll increase sales of 
washing machines.<br>
<b><br>
Profitable amusements</b><br>
The most obvious benefit of complementary innovations is an increase in sales of 
your main products. But there are other paybacks. As the Michelin Guides became 
popular, for instance, they proved to be a powerful, proprietary channel for 
marketing and brand building. In time, the guides became successful and 
lucrative in their own right.<br>
<br>
Complementary innovations can bring operating benefits, too. Consider the early 
streetcar-system operators in the United States cities, who struggled with 
earning a decent return on their capital-intensive rail networks. Although many 
people travelled on the lines during weekday rush hours, few rode them at other 
times. The imbalance in demand undermined the profitability of the operators.<br>
<br>
To boost ridership during nonpeak hours, the operators hit on a brilliant idea: 
Build amusement parks outside city centres. In the summer of 1897, for example, 
Boston’s Commonwealth Avenue Street Railway opened Norumbega Park, complete with 
a zoo, a theater and a carousel, at the end of its line in the suburb of Newton.<br>
<br>
By 1901, notes historian David Nye, over half the country’s urban-transit 
companies had opened such ‘trolley parks’. They did more than increase 
ridership; they also made the companies more capital-efficient by increasing 
capacity utilisation of the electricity generators that powered the trains.<br>
<br>
Smart innovations in complements can also deal a blow to competitors. Take Intel 
and the Wi-Fi networking market. At the end of the 1990s, the biggest producer 
of the semiconductors, used for Wi-Fi connections in PCs, was a small company 
named Intersil. The rapidly growing popularity of Wi-Fi made Intersil a threat 
to Intel’s control over the chipsets that run PCs.<br>
<br>
In response, Intel introduced its own Wi-Fi chip — the Centrino — and offered it 
to PC manufacturers at a dirt-cheap price. According to press reports, Intel 
sold the Centrino for considerably less than it cost to produce it. The collapse 
in prices quickly destroyed Intersil’s business.<br>
<br>
Why could Intel afford to sell the Centrino at a loss? Because Wi-Fi was a 
complement to its core microprocessor business. By helping to make Wi-Fi service 
cheap, Intel encouraged potential customers to buy laptops rather than desktops 
— and Intel made far more profit by selling chipsets for laptops than for 
desktops.<br>
<b><br>
FIVE GOOD QUESTIONS</b><br>
How does a company uncover innovation opportunities in complements? Consider 
these five questions:</font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">1. What complements are constraining demand in 
your markets? Examine all the products and services used in conjunction with 
your own. Are any in short supply? Are any expensive or difficult for customers 
to find? Perhaps your company can spur advances in the production or 
distribution of complements to make them cheaper and more plentiful.<br>
2. What new product might boost demand for your core offerings? Like the early 
streetcar operators, think beyond the current ways that customers use your 
products.<br>
<br>
3. Would providing information about complements, as the Michelin brothers did, 
boost sales of your core product?<br>
<br>
4. Would you learn valuable lessons by innovating in complements? Complements 
may help you operate more efficiently or design superior products and services 
as well as expand your sales. Hewlett-Packard’s expertise in formulating ink, 
for example, enables it to manufacture better printers, and vice versa.<br>
<br>
5. Do you have competitors whose fortunes are tightly tied to the price of 
complements? Analyse your competitors to see if any of them earn a lot of money 
by selling products that are complements to your own. If so, innovations that 
drive down the price of those complements will erode your competitor’s profits 
while boosting your own sales.<br>
<br>
Of course, such strategies can cut both ways. Your own core products are 
probably complements to some other company’s offerings. It would be wise, 
therefore, to think defensively as well as offensively. How vulnerable are you 
to innovations that would make your products cheaper and more plentiful?<br>
<br>
There are two kinds of innovation that pay off for companies: those that provide 
benefits to customers, and those that raise barriers to competitors. 
Investigating complements can lead to breakthroughs in both.<br>
<br>
<i>Nicholas G. Carr, a contributing editor to Strategy + Business magazine, 
author of Does IT Matter? Information Technology and the Corrosion of 
Competitive Advantage (Harvard Business School Press, 2004). He is working on a 
book about the future of the computer.</i></font></p>
