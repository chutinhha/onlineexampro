            </font><font face="Verdana" size="4"><b>Changing people’s perception</b></font><font face="Verdana" size="1"><br>
			<b>Oman United Agencies GM Marek Sheridan says the days of being a 
			‘generalist’ is over — in a candid tête-á-tête with Abhijit Sinha<br>
			<br>
			</b>
			</font><font face="Verdana" size="4"><b>
            <img border="1" src="atthehelm.jpg" align="left" width="99" height="137" vspace="2">C</b></font><font face="Verdana" size="1">hange 
			Management’ is his forte — and his mantra: “Make a difference and 
			enjoy it.” UK national Marek Sheridan (39) has been in the Middle 
			East for 14 long years now. “December 2004, I was brought here by my 
			bosses in Dubai and our local shareholders to be an ‘instigator of 
			change’,” reflects the General Manager of Oman United Agencies 
			(OUA).<br>
			<br>
			Sheridan was coming over from Maritime and Mercantile International 
			(MMI), part of the Emirates Group, after a three-and-a-half year 
			stint as head of corporate marketing and liquor marketing.</font></p>
			<p align="justify" style="margin-left: 5px; margin-right: 10px">
			<font face="Verdana" size="1">As a management leader, he has “evolved” his own vision of where a 
			business can go — and communicates this to his team, the managers 
			and the staff. “Once you have done that, it’s quite powerful — as 
			everyone then is pulling in the same direction.” An MBA in 
			Management from Leicester (UK) and applying business development 
			models of Michael Porter, albeit claiming to be “not much a man for 
			management gurus,” has come in handy to focus on this vision.<br>
			<br>
			“We are an Omani company (operating for over 80 years) with a 
			diverse portfolio — and a 282 workforce including some Germans and a 
			Brit: me. My job is to make sure we have specialist managers and 
			specialised infrastructure in the categories we operate,” says 
			Sheridan who predicts growth in all the OUA’s sectors: Consumer 
			Goods, Beverages, Travel with spin-offs in the form of Gulf Ventures 
			and Leisure Travel, plus servicing the Associate Companies.<br>
			<br>
			Year 2005 saw a 37 per cent increase in business. “This year, we are 
			targetting about 68 per cent. That means some investment — like 
			setting up the Gulf Ventures. Also looking at new sector ‘Leisure 
			Retail’ — bars and restaurants. That’s a bit embryonic. There are 
			some plans on the Travel sector too. But that’s still being 
			discussed.”<br>
			<br>
			As General Manager, Sheridan’s take on the Equation of Power is: 
			“Understanding the direction, communicating it properly and finding 
			the support service.” On the marketing and business plans of OUA, 
			the key points are: “Add value and make a difference; Remain 
			focused; Never lose touch with the culture and ethics of the land.”<br>
			<br>
			Sheridan explains, “OUA’s business diversity has given us a real 
			strong foundation for growing in Oman — and the recent dynamic 
			growth, with lots of investment boosted by oil revenue, has fuelled 
			our drive.”<br>
			<br>
			Harking back to his ‘Change Management’ strategy, he explains: 
			“Previously, it was ‘One size fits all’ — that is, be a 
			‘generalist’. I am saying something very much different. That your 
			values can translate across the units. The way you do things and how 
			you achieve your goals has to be specialised.”<br>
			<br>
			The right people, system and infrastructure — to target what would 
			soon be a much more sophisticated market place. For Sheridan, 
			“management tools and tactics have become much more specific and 
			individual oriented.”<br>
			<br>
			Identifying future markets is not so difficult, “as Oman is not a 
			huge market place and opportunities are easy to spot — and equally 
			easy to miss.” <br>
			<br>
			Employing external agencies to assess change is done occasionally 
			“to support an idea or innovation, but the decision about where we 
			want to go is done internally.” Sheridan says rushing in on gut 
			feeling is bad business tactics.<br>
			<br>
			“We must fully identify the opportunity before proceeding.” That, 
			precisely, is Sheridan’s job profile — and “to advise my 
			shareholders how best to achieve them to provide for the future as 
			well.”</font></p>
