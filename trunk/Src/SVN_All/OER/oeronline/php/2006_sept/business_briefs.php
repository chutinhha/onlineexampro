<font face="Verdana" size="1"><br>
<b>Gulf Air Muscat traffic jumps by 30.1% </b><br>
<br>
<img border="0" src="gulf_air.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="160" height="117">Introduction 
of a two-hub strategy earlier this year, and the increase in Gulf Air flights to 
and from Oman and Bahrain, has produced a record passenger traffic at both the 
airline’s hubs. Releasing passenger figures for the year till June 2006, Gulf 
Air has announced a 30.1 per cent increase to 1,137,482 travellers through 
Muscat, and a 32.3 per cent increase to 2,181,227 travellers through Bahrain, 
compared to last year’s figures. <br>
<br>
The airline’s outgoing President and Chief Executive James Hogan said, “In the 
first quarter, we recorded growth of 20.6 and 24.1 per cent, respectively. 
However, when the full impact of the two-hub model came into effect with the 
summer schedule in the second quarter, the growth increased to 39 per cent in 
Oman and 41.3 per cent in Bahrain. Few airports in the world could handle 
year-on-year growth of even half that level.”<br>
<br>
Hogan added, “We have started to see significant trends coming from our two-hub 
operation, in terms of crewing, aircraft utilisation and network connections — 
with the full impact becoming apparent in April following the introduction of 
the summer schedule. As a result, we expect an overall positive impact on our 
services this year. While some challenges and uncertainties remain in 2006, 
notably fuel costs, I believe we have a sound basis for continued positive 
development in the future.”<br>
<br>
Since the introduction of the two-hub strategy, the number of direct non-stop 
Gulf Air flights to and from Seeb International Airport has increased by 45.5 
per cent over last year and the airline now accounts for nearly 60 per cent of 
the passengers at the airport, as per a Gulf Air press statement.</font></td>
