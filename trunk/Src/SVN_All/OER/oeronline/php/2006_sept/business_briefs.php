<font face="Verdana" size="1"><b><br>
Government revenue up by 39.8%</b><br>
<br>
The Omani government’s revenue increased by 39.8 per cent during the first six 
months of this year to RO3,420 million, against RO2,446.5 million during the 
corresponding period in 2005, according to the monthly statistical bulletin 
issued by the Ministry of National Economy. The rise in revenue was attributed 
to a 33.5 per cent surge in the Sultanate’s oil net revenues which rose to 
RO2,542 million, compared to RO1,904.4 million during the same period in 2005, 
and a 120.2 per cent rise in natural gas revenues which stood at RO305.7 
million, against RO138.8 million during the same period in 2005. The 
government’s general expenditure rose by 3.6 per cent during the first six 
months of this year to RO1,892 million, against RO1,826.6 million during the 
same period in 2005. <br>
<br>
The rise in the general expenditure resulted from a 3.4 per cent increase in 
current spending which stood at RO1,467.8 million, as against RO1,419.3 million 
during the same period in 2005, and a 37.3 per cent surge in contribution and 
support for the private sector which amounted to RO32 million, compared to 
RO23.3 million at the end of June 2005. The state general budget also saw a 
surplus of RO354.8 million during the first six months of this year, compared to 
a surplus of RO532.8 million during the same period in 2005.<br>
