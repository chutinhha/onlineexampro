<br>
</font><b><font face="Verdana" style="font-size: 14pt">SmartOffice Smart 
Solution<br>
New Age Integrated Info-Tech</font></b><font face="Verdana" size="1"><b><br>
Cost-effective SmartOffice tools are a judicious mix of IT, Ergonomics and Brand 
Awareness, says Sarada Vishnubhatla<br>
</b><br>
<img border="0" src="SmartOffice.jpg" align="right" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="160" height="126"></font><b><font face="Verdana" size="2">I</font></b><font face="Verdana" size="1">nformation 
&amp; Communication Technology (ICT) has enabled more and more peoples’ lives the 
world over to be driven by automated solutions. The information highway is now 
all about time compression, confidentiality, speed and nano-technology. The 
seamless quality of Smart Solutions is evident to all. Policy makers, business 
tycoons, finance controllers the world over are embracing e-enabling services to 
widen their reach and find cost-effective work methods.<br>
<br>
Oman is a growing economy and is gradually absorbing the flexibility e-Smartness 
offers. There are teething problems too. “Most customers refuse to believe that 
the smart machines can do multi-tasking. They think it is rocket science,” feels 
Tajj Deen, Manager (Office Automation), Jawad Sultan Technologies.<br>
<br>
<img border="0" src="Conference.jpg" align="right" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="160" height="119">An 
organisation is made smart by graduating from paper to e-ways of working in the 
audio, video and web categories. Mohsin Haider Darwish’s (MHD) Manmohan Singh 
Rehsi, General Manager, Office Automation and Supplies, says, “Smart use of web, 
internet, web technology leads an office to become a smart e-office. It is a 
gradual process to upgrade to an e-based smart office. It results in higher 
productivity, transparency, and flexibility — and is not location-based. But 
office culture too plays an important role. Three things are important for any 
Smart Office: people, process and technology.”<br>
<br>
Conference calling, web conferencing, unified messaging, voice mail, replay 
line, fax on demand, virtual office, software solutions are some areas where 
cutting edge technology is required to make an office e-smart. Bahwan IT’s Anil 
Kumar, Sales Manager, describes the role of the group, “We are first a 
consultancy/integration solution provider. We carry the top IT brands and 
partner with the world’s leading solution providers and consultants — providing 
a portfolio of turnkey solutions and services to our customers. We are in the 
area of enterprise solutions, networking/security practice, infrastructure 
solutions, data capture and software solutions. Our focus starts from consulting 
on these practices.”<br>
<br>
Nowadays, office interiors are being renovated to accommodate the demands of 
such communication, keeping aesthetics and space management as top priority. 
Overhead projectors, plasma screens, surround sound systems, sleek microphones, 
video cameras and more gizmos find discreet space in a smart office. Utilities 
such as mobile phones, computers or laptops, telephone network, printers, 
scanners, fax machines and photocopiers use or share computer based data and 
software applications for higher productivity, saving on time. <br>
<br>
<b>Cutting Edge Technology</b><br>
Technologies such as DECT, WiFi, ADSL or ISDN, GSM and Bluetooth are used 
worldwide for a speedier work output — with reduced encumbrances such as cables, 
limited coverage areas and the like. Devan Thuppali, General Manager, OHI 
Electronics, says, “In the world of smart telephony, DECT is becoming a 
worldwide standard for short-range cordless mobility, and can be adapted for 
many other cordless applications like Wireless Local Loop and Cordless PBX. 
Interest in the DECT standard is global. Offices, warehouse, hospitality 
segments are all now looking at wireless communications equipment and the demand 
for this technology is booming.”<br>
<br>
Brands such as JVC, NEC, LG, Ricoh, Samsung, Panasonic, Philips, Toshiba, Dell 
and Sony among others cater to varied needs with a range of products. Big, 
medium or small organisations can have ‘smart gadgets’ depending on the budget. 
In Oman, companies like MHD, Bahwan IT, OMASCO and OHI Electronics offer 
integrated services and e-solutions to the organisations.<br>
<br>
Rehsi of MHD says, “The Computers, Office Automation and Telecommunications 
(COAT) division at MHD is focused on providing end-to-end technology solutions 
to its customers. We are a ‘One Stop Shop’ for all technology needs of all our 
customers.” OHI’s Thuppali says, “The difference that OHI Electronics makes is 
about our capability to offer a customised turnkey-based integrated approach to 
a client-specific need.” <br>
<br>
<b>Smart Products &amp; Services</b> <br>
Staying connected is critical in a company’s ability to respond to a customer’s 
changing needs, with the latest technology to work around office timings. The 
SmartOffice equipments now come in portable sizes too, so that the user may even 
cart it along to another part of the continent — and never have to miss out on 
an important meeting or lose out on a business deal.<br>
<br>
New technology has also enabled a combination of gadgets to be geared for 
multi-tasking. SmartOffice gadgets like the Germany-made photocopier Utax can 
also function as a printer and a scanner. Take Canon 3570 multi-function machine 
or Konica Minolta copiers. Besides being web-enabled, the new age printers and 
scanners can be connected on LAN for streamlining office work, reducing clutter 
and confusion.<br>
<br>
“An executive needs information to be readily available. Sitting at home, he 
should be able to view his warehouse floor, showrooms or even his overseas 
outlets or head office and get himself updated by the minute. Integration is the 
buzzword. So suppliers like Panasonic are moving towards IP camera solutions,” 
opines Nadkarni of OMASCO.<br>
<br>
Brands such as Magicolor, HP, Canon, Heidelberg Speedmaster, Lexmark, Hitachi, 
Ricoh etc are all geared up to offer facilities like batch scannings and scan to 
several destinations such as remote printer, email. Document management software 
is possible nowadays. LCD projectors and Plasma TVs are extensively used these 
days in conference rooms and auditoria. These can be linked to a computer or a 
laptop and public events, presentations and seminars can be conducted. <br>
<br>
<b>Ergonomics</b><br>
Worldwide, the need for ergonomic furniture is considered a sensitive issue. A 
functional SmartOffice has employees who are well-trained and healthy. A 
UK-based research shows that work related sickness costs more than US$21 billion 
every year. Of this, the most is attributed to back injury. Spending long hours 
sitting at desks in offices, and at home in non-ergonomic furniture, is the most 
important factor contributing to back pain of the user.<br>
<br>
A major investment for any company is its employee. Keeping such an employee 
healthy, especially the ones who are tied to their desks and computers for 
better part of the day becomes a priority for the company to reduce overheads. 
Countries in the GCC — and especially Oman — find a new orientation towards 
ergonomic office furniture that offers perfect support to an employee who is 
breaking his back towards meeting a deadline. Investment aimed at safeguarding 
the interests of the employees is worth every baiza spent. <br>
<br>
Ergonomically designed furniture not only encourages free movement, but also 
follows and supports the body naturally in every posture, without user 
intervention. Omani companies like Teejan, Alafsoor, Fahmy and Bahwan, Al Fajer 
and Khimjis cater to organisations that care for their employees’ health.<br>
<br>
Shankar Naganathan, Senior Divisional Manager, Furniture Division of Khimjis 
says,“We offer all the facilities needed to set-up a smart workplace under one 
roof. Be it air conditioning, lighting, civil works, data and power lines, or 
the furniture and fittings, Khimji’s have pioneered the concept of modular 
office — where the entire office can be swiftly redesigned to meet growing needs 
or any changes. Some of our top clientele includes corporates like Oman Oil, 
P&amp;G, McDonald, Sohar Refinery, OIB, Oman Mobile, and various government 
ministries. We also offer office automation products such as note counting 
machines, mobile filing systems both mechanical and electrical, industrial 
storage and shelving solutions to meet specific warehousing needs for smart 
space management.”<br>
<br>
<b>Affordability</b><br>
Top-notch companies in Oman believe that the cost for integrated solutions is 
extremely affordable, and an organisation’s image and its communication 
capabilities are enhanced. Thuppali of OHI Electronics says, “These products 
have become a must-need, considering that almost all presentations are made from 
either a laptop/PC or a video source. When you address a large gathering or 
panel, you need a projected image to enable many people view the same 
effectively.”<br>
<br>
Rehsi of MHD agrees, “Easier processes, smooth supply chains and total 
management of office brings down overheads. Returns are beneficial but they take 
time. Patience, vision and commitment on part of the management are important. 
Companies spending on technology do not expect immediate returns. So cost as 
such is not a deterrent.”<br>
<br>
<b>Smart Training</b><br>
Training should be ongoing and based on functional needs identified in an 
organisation’s strategic plan. Options range from ‘How to…’ books to computer 
and web-based training to classroom instruction. MHD and OMASCO, being 
end-to-end solution providers, believe in a work culture that allows its workers 
to be fully trained in the new technology that the management has agreed to 
adopt. Rehsi says, “Innovation in how to use technology is important. This is 
more people oriented. If the culture is there and a company is innovative, only 
then a SmartOffice is actually that.”<br>
<br>
<b>Being Brand Smart</b><br>
With everything in place, how does the consumer ensure that his choice of an 
integrated solution from a particular brand/company is the right one? The 
question to ask here is “Are brands the users too?”<br>
<br>
It is not only the individuals or business houses that depend on ICT. The 
solution providers themselves are the biggest users too. Anil Kumar of Bahwan IT 
says, “We use most of the products in-house even before selling in the market. 
Bahwan IT office is a showcase for all international products used in the region 
for information consolidation and support services. This gives us the advantage 
to understand the products and the services we offer in the market best even 
before we take them to our customers.”<br>
<br>
Genetco-Canon’s Srikanth maintains that it all depends on the person’s office 
needs. He says, “We lead with example. We use scanners for archiving our 
accounts, indexing. We print all our large posters and banners using our laser 
printers. This convinces our customers that our products are good.”<br>
<br>
A smart office is all about information access anywhere, anytime. The business 
community the world over and in Oman counts on it.</font></p>
