            <font face="Verdana" style="font-size: 16pt"><b>MANAGEMENT GURUS</b></font><b><font face="Verdana" style="font-size: 11pt"><br>
            </font><font face="Verdana" size="2">Presenting Short Cuts To Professional ‘Nirvana’</font></b><font face="Verdana" size="1"><br>
            <br>
            <b>Corporate Oman is catching up with rest of the world in rushing 
            to management gurus who promise to change destinies with their 
            simple ideas. Akshay Bhatnagar and Divya Kumar probe their impact
            <br>
            </b><br>
            </font>
            <font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="cover1.jpg" align="left" hspace="5" vspace="2">A</b></font><font face="Verdana" size="1">dam 
            Smith, the renowned Scottish economist, mesmerised the global 
            corporate community with his book Wealth of Nations way back in 
            1776. Currents of Adam Smith ran through David Ricardo and Karl Marx 
            in the 19th century, and through Robert Keynes and Thomas Friedman 
            in the 20th. But it has now taken the shape of modern day management 
            (business and motivational) gurus who are jetsetting across the 
            world — hobnobbing with business managers, political leaders and 
            entertainment stars with equal aplomb. Promising to transform the 
            fortunes of individuals and institutions with their simple yet 
            analytical thinking, they have turned into an industry themselves!<br>
            <br>
            The management guru trend picked up momentum in the ‘80s — mainly in 
            the US and Europe market. Growing competition and changing market 
            equations in the global village made the global corporates line up 
            to listen to these spin doctors, with the objective of safeguarding 
            their business interests. The rest of the world followed suit in the 
            ‘90s. Corporate Oman which hitherto was flocking to Dubai, the focal 
            point for the global management gurus in the Gulf region, realised 
            the need to make these gurus visit the Sultanate to spread the 
            benefits from their know-ledge on a broad level here.<br>
            <b><br>
            Spin doctors in Oman</b><br>
            Management thinkers have been coming to Oman before too... But their 
            frequency and stature have gone up substantially in the last two 
            years. Earlier, it was unthinkable to have gurus such as Michael E. 
            Porter, Tom Peters, Ron Kaufman, Jack Canfield and Deepak Chopra 
            dropping in at Muscat in quick succession. More and more event 
            management and human resource development agencies are coming 
            forward to arrange, and manage, interactions between the gurus and 
            Corporate Oman right here in Muscat. Why this sudden change? <br>
            <br>
            “This marked shift is a cumulative effect of various developments in 
            the Sultanate. The competition has gone up. An increased integration 
            with the global market has led to more exposure for the 
            organisations and individuals. With more international companies 
            coming in, the realisation has set in that we need to change, learn 
            and know new things,” says C.E. Potnis, Group General Manager, Oman 
            Resources Development Consultants LLC (ORDC), which brought Jack 
            Canfield to Oman in December last year. <br>
            <br>
            It is said that half of the vision gets lost in the implementation. 
            Most of the organisations don’t have a problem in the strategy 
            planning; the problem comes in its execution. On a number of 
            occasions the basic aspects to achieve the objectives are lost in 
            the process of strategy implementation. That’s where the management 
            gurus step in.<br>
            <b><br>
            Who benefits</b><br>
            So how effective are these management gurus in bringing a change in 
            the culture and productivity of a company? There are divergent views 
            on this subject. Broadly speaking, beyond immediate business gains, 
            such sessions place Oman on the global map and attract the audience 
            from nearby countries to add to the tourism sector of the Sultanate. 
            They also impact the thinking of the youth, especially Omanis 
            charting out their career path and developing their own style of 
            management and leadership. <br>
            <br>
            Raphael Parambi, CEO, Muscat Finance Company, who has attended many 
            sessions with these gurus says, “At my age, these gentlemen 
            (management gurus) are not going to tell me anything that I don’t 
            know. But it refreshes the whole thing in my mind, hearing it again. 
            Hearing a new slant adds value. People like Tom Peters don’t add 
            much technical knowledge. But there’s a certain inspirational aspect 
            to it — talking of change, the need to change, how fast the world is 
            changing, how we’ll get left behind. Those kind of things which are 
            not generally taught at traditional management schools. Same is the 
            case with Jack Canfield whom I like for his inspirational and 
            positive thinking — always worth stepping back and thinking about.”<br>
            <br>
            N. Ramesh, CEO, Dalma Energy LLC agrees with Parambi. He says, 
            “Generally, we find such sittings quite productive. Take, for 
            example, a recent seminar Bricks to Bridges by Robin Speculand. My 
            team and I felt that the seminar re-emphasised things we have known 
            all along, but which tend to get forgotten in our daily activities. 
            The opportunity to stand back and reflect on how we are running our 
            business, and compare the problems we are facing with those being 
            faced by other organisations, was really useful.”<br>
            <br>
            On the other hand, a general manager of a large automotive sales 
            firm believes that these management gurus are not as effective as 
            they are made out to be. He says, “To make my team members attend a 
            session with a guru means that the company has to invest in their 
            entry fee — and secondly, they will be away from the duty for that 
            many hours. I don’t expect them to learn anything great in a single 
            day which is going to impact my company’s productivity drastically. 
            If their education and work experience has not taught them enough 
            all these years, then how can I expect them to change in a single 
            session!”<br>
            <br>
            Potnis disagrees completely with this logic. “Many a time in life, 
            what your entire education may not teach you, one event, one 
            setback, one success, one interaction, one audience, one handshake 
            can really change your life. If we look back on our lives, there is 
            always one incident which changes the way we think. These people 
            (gurus) bring in the world of experience with them; they bring in a 
            unique thought process. They have a great skill of articulating and 
            putting it across, using a lot of anecdotes and stories — and it 
            becomes so inspiring. Out of an audience of hundred, even if only 
            two people are benefitted, it’s a tremendous contribution — because 
            it creates an environment and culture. The impact becomes visible 
            over a period of time.”<br>
            <br>
            S. Suresh, Corporate Head of Al-Hashar Group of Companies, offers a 
            balanced view. “It will be naive on our part to assume that a day or 
            a session of talk by any management guru can change the destiny of 
            professionals or companies. To me, a session with a management guru 
            is like reading a book. The effect is very subjective and completely 
            dependent on the reader of the book. The key advantage is that these 
            sessions bring in a new perspective to any problem, and to that 
            extent are very useful.”<br>
            Amit Sampath, Director, Gazelle International that brought in Ron 
            Kaufman to Oman, also believes that a lot depends upon the 
            individual — how much he is able to understand what the guru is 
            trying to convey. “At times, there are disappointments too. For 
            example, some people gave the feedback that Ron Kaufman said what 
            they already knew. On the contrary, Kaufman was trying to say that 
            sometimes when you’re already into the business, you tend to ignore 
            the basic requirements, and that’s what he focused on.<br>
            <br>
            So Kaufman asked the sales persons, ‘What do you do when the 
            customer comes in?’ Some said that we note down their requirements. 
            Well, that’s exactly what you’re not supposed to do, he said. He 
            emphasised that when the customer comes in, you’re supposed to make 
            them comfortable saying ‘Hi, what’s the problem? How can I help 
            you?’ So, sometimes people miss the entire idea of the event. I 
            mean, there’s always mixed feelings about any event. There’s no way 
            you can satisfy 300 people at the same time,” concluded Sampath.</font></p>
