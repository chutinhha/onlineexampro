                  <p align="justify" style="margin: 5px">
                  <font face="Verdana" size="1"><br>
                  Going by the feedback from the companies and individuals, the 
                  hurdles in getting top-notch management gurus to Oman pertains 
                  to the gurus themselves as well as the market here. The 
                  management gurus commanding immense popularity and demand all 
                  over the world have become cult figures. Naturally, their 
                  demands are also very high. Take, for example, Michael E. 
                  Porter, the world’s No.1 management guru as per Accenture (see 
                  box Top 10 Business Gurus).<br>
                  According to a senior executive closely associated with the 
                  visit of Porter to Oman: “It took us almost two years to fly 
                  him here, that too on an invitation from the Minister of 
                  Commerce &amp; Industry. He charged more than RO50,000 as his 
                  fees. He fixed the date and timing of the visit. He came on 
                  the condition that he must fly back the same night to Tokyo on 
                  a chartered flight. He spoke to me for just 30 seconds after 
                  the presentation, as if he was doing a big favour to me. I 
                  think he believes that he is God!”<br>
                  <br>
                  While the fees range from RO20,000 to RO60,000, depending upon 
                  the guru’s stature, boarding and lodging charges come steep 
                  too. Add marketing and logistics cost, the total budget for 
                  the event goes very high. The organisers rely on sponsorships 
                  and participation fees to meet the overall expenses.<br>
                  <br>
                  Raphael Parambi, CEO, Muscat Finance Co. says, “It’s not easy 
                  to find people who appreciate your effort by backing you. In 
                  this market a lot of people don’t like to pay to attend the 
                  guru sessions. On the contrary, many of them go to such kind 
                  of paid events in Dubai! Of course, the top-end gurus manage 
                  to attract a paid audience in Oman. But those who are of 
                  slightly less stature fail to attract enough paid audience.”<br>
                  <br>
                  C.E. Potnis, Group General Manager, Oman Resources Development 
                  Consultants LLC (ORDC) adds, “The corporates’ attitude is 
                  definitely changing. But Oman still has a long way to go, 
                  compared to a market like Dubai. The government and the 
                  private sector should recognise the importance of holding such 
                  events in the Sultanate. The increasing influx of gurus to 
                  Oman also points out that we are moving in the right 
                  direction. The need of the hour is to accelerate the pace.”<br>
                  <br>
                  Another challenge is the small size of the market. In bigger 
                  markets, the sheer number of companies is much higher, 
                  compared to Oman. Also, the event organisers have a large pool 
                  of companies to target for sponsorship and delegation fees. 
                  But that’s not the case in Oman.<br>
                  <br>
                  “We can’t expect the same set of companies to back us six 
                  times a year if we bring more gurus to Oman. There has to be 
                  enough number of large corporates who could invest on a 
                  rotational basis without feeling the pinch. In Oman, we rush 
                  to the same telecom, banking or automotive firms to endorse 
                  our events,” clarifies Amit Sampath, Director, Gazelle 
                  International.</font></td>
