            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Guru’s feedback</b><br>
            Last December, OER met Jack Canfield on his maiden trip to Oman. We 
            asked him, “Do you think management gurus can really change lives of 
            the people?” He said, “It depends upon individual to individual in 
            the audience. Like, in a school, all the students listen and try to 
            follow the lecture of the teacher. But finally the end result, or 
            the learning, varies from student to student. The same principle 
            applies in our case as well.”<br>
            He also shared some of his success stories with us. “Some time back, 
            a gentleman who was into public speaking attended my seven-day long 
            workshop on learning success principles. He said, ‘After attending 
            your workshop I have become a millionaire in two years. I have 
            tripled my public speaking business. More than that, my children say 
            that they have got a new father. They say that I have changed after 
            coming from the workshop. They say that I listen to them and don’t 
            lecture them. I understand that their needs 
            are different. I don’t give them everything that they want, but I’m 
            more understanding now’.”<br>
            <br>
            Canfield told OER: “A few years back in one of my workshops I had a 
            person who was working as a sales trainer with a large corporation, 
            earning US$140,000 a year. He was stuck in that position for quite 
            long, and he didn’t have a clue why he had not been able to progress 
            further in his life. We did an exercise with him to become more 
            successful in life. One year later he was making US$750,000 a year, 
            working just three days a week.” Need we say more!<br>
            <b><br>
            Knowledge sharing</b><br>
            Most of the time, the knowledge gained is restricted to the 
            individuals who attend the guru’s session. On most of the occasions, 
            people come, enjoy the moments, do social networking, and the next 
            day, crack jokes on the session with their colleagues. The majority 
            of senior managers admitted off the record that the entire process, 
            of converting the knowledge gained into productivity for the 
            company, usually went for a toss. Why? Due to the lack of a proper 
            mechanism to instill these lessons among others in the company who 
            didn’t get an opportunity to attend the sessions.<br>
            <br>
            “Generally the top management of a company gets to attend these 
            premium events. Ideally, the senior managers should hold sessions 
            with their team members in a structured manner post-event — to 
            disseminate the knowledge and discuss ways to adopt the learning so 
            as to improve the company’s operational efficiency and strategic 
            planning. But that rarely happens,” shares a customer care executive 
            working in a prominent bank. It is quite discouraging that Corporate 
            Oman fails to take optimum advantage from the presence of management 
            stalwarts, as the organisers take huge pains to bring them here. 
            (See box Bottlenecks for the Organisers)<br>
            <b><br>
            Follow-up mechanism</b><br>
            What happens when one faces practical problems in following what the 
            gurus had prescribed? Whom does one rely on for the solutions? We 
            can’t imagine managing directors or chief executives asking their 
            colleagues for solutions! The solution has to come from the guru 
            himself. Though all gurus leave their contact details, we can’t 
            expect them to reply to each individual query.<br>
            <br>
            “That’s not the case,” disagrees Sampath of Gazelle International. 
            “Ron Kaufman is quite prompt in replying to anyone’s queries. He 
            generally takes just 24 hours to respond. He likes to continue the 
            relationship post-event also, to understand and help his audience to 
            following his advice.” But not many are like Kaufman. <br>
            <b><br>
            Language barrier</b><br>
            Generally, most of the management gurus are English speaking. They 
            deliver their discourses in English, which alienates the Arabic 
            speaking public as many may not follow English perfectly. It means 
            that only the English speaking expatriates and Omanis benefit from 
            these gurus. The problem can be tackled in two ways. One is to 
            invite Arabic speaking management gurus as well — however there may 
            be a shortage of them in the top bracket.<br>
            <br>
            Gazelle International is planning to bring in some Arabic speaking 
            management spin doctors to Oman. But obviously they would be of a 
            lesser stature compared to stalwarts such as Michael Porter, Tom 
            Peters or Jack Canfield. A second option could be an arrangement of 
            simultaneous Arabic translation. “We evaluated doing simultaneous 
            translation from English to Arabic. But we shelved the idea as we 
            felt that the effectiveness and impact of the talk would just go 
            away,” says Potnis, ORDC Group General Manager.<br>
            <b><br>
            The way forward</b><br>
            In discussions with the corporate honchos over the last few months, 
            OER found that there is an additional element of disgruntlement 
            among them over such events. The main grouse is related to the value 
            proposition vis-a-vis cost factor. Some of the events have high 
            entry fees (in excess of RO200 per participant in some cases), which 
            they find exorbitant.<br>
            <br>
            Another telling point was that many of these highflying gurus take 
            no pains to factorise in Oman while giving their presentations — 
            thus reducing the degree of effectiveness for corporate Oman. 
            Majority of the gurus tend to recycle one of their existing 
            presentations, made umpteen number of times in other places already. 
            Perhaps, for many of them Muscat trip is just an add-on to their 
            trip to Dubai, opined a senior manager who felt shortchanged. “They 
            should not take corporate Oman for granted,” was the candid comment.
            <br>
            Another way to look at this problem is not to invite the guru 
            himself. A management doyen here could well present the ideas of the 
            gurus to the local corporate community. That would help make such 
            events more affordable for the companies — and a higher number of 
            middle and junior level professionals would gain from the knowledge.<br>
            <br>
            The gurus’ effectiveness can be gauged when, as it happens in the 
            other markets, more management professionals in Oman start paying 
            from their own pockets to attend these high-profile sessions S — 
            instead of relying on complimentary entry or company sponsorship.<br>
            <br>
            In spite of the teething troubles, most of these “management guru” 
            events have been well received in Oman. And the trend favours more 
            of the same. The economy is on a roll, leading to massive 
            investments in infrastructure developments activities. A renewed 
            thrust has been given to key sectors such as telecom, tourism, 
            retailing, banking and finance, and manufacturing industries besides 
            trading. Both the government and private sector are embarking on an 
            Omanisation drive.<br>
            <br>
            All this calls for the training and development at different levels 
            — whether it is related to leadership and strategic planning for the 
            top-level managers, or skill upgradation and motivational uplifting 
            for mid level managers. Though change is definitely taking place, it 
            is still treated as more of an expense than an investment for the 
            company. The faster the mindset changes, the better it is for 
            Corporate Oman.</font></p>
