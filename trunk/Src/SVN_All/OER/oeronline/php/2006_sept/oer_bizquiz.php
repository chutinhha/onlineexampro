                            <b>The answers to OER July 2006 Biz Quiz:</b><br>
                            <br>
                            <b>1. Vertical integration </b><br>
                            When firms at different stages of the production 
                            chain merge.<br>
                            <b><br>
                            2. Horizontal equity</b><br>
                            Fairness in relation to equal treatment of different 
                            people who are in the same circumstances.<br>
                            <br>
                            <b>3. Veblen goods </b><br>
                            Named after the American economist Thorstein Bunde 
                            Veblen, these are goods (products) that may have an 
                            upward sloping demand curve because of their 
                            conspicuous consumption. In other words, people may 
                            buy more of this product because it is more 
                            expensive.<br>
                            <br>
                            <b>4. Human Development Index </b><br>
                            Introduced by the UN in 1990, the HDI takes into 
                            account not only the goods and services produced, 
                            but also the ability of a population to use these 
                            and the time they have to enjoy them. It is a 
                            composite index based on Purchasing Power Parity 
                            (worked out from Gross Domestic Production in Per 
                            Capita terms), life expectancy at birth and 
                            educational achievement that measures socio-economic 
                            development.<br>
                            <br>
                            <b>5. Quota sampling</b><br>
                            A sampling method widely used in opinion polls and 
                            market research. Interviewers are each given a quota 
                            of subjects of specified type to attempt to recruit. 
                            For example, an interviewer might be told to go out 
                            and select 20 adult men and 20 adult women, 10 
                            teenage girls and 10 teenage boys so that they could 
                            interview them about their television viewing habit.<br>
