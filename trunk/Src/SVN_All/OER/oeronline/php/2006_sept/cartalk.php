<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">A class apart<br>
</font></b><font face="Verdana" size="1"><b>Stylish, safe and technologically 
advanced — the Mercedes-Benz 
S-Class is a pure driving pleasure, finds Anne Kurian</b></font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><br>
<img border="0" src="s_class.jpg" hspace="5" vspace="2" style="border-style:dotted; border-color:#C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px"></font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Ever 
been inside a car and felt that it was made with you in mind? As if the 
automaker knew just about everything that you wanted in a car and created it to 
suit your whims and fancies? That’s exactly what we felt when we took out the 
Mercedes Benz S Class for a test-drive. Mercedes Benz had taken prime aspects 
like comfort, luxury and safety to a new level with the S Class.<br>
<br>
Stylish as ever, the S-Class takes your breath away at first glance itself. The 
regal design of the youngest member from the German automaker in no way hides 
the fact that the S-Class has a bold racy feel to it. A looker, this one 
definitely is! Apart from the stylish appearance and top-end features, the 
S-Class boasts of comfortable and safe driving during the day as well as the 
night. The German auto giant has incorporated a host of safety features that 
enhance the driving experience, especially at night.<br>
<br>
<b>Style &amp; Comfort<br>
</b><br>
The S-Class scores in the looks and styling department on the interior as well 
as the exterior. It’s hard to miss out the flared wheel arches that lend the 
luxury sedan that sporty touch. Much like its older siblings, it’s the 
distinctive styling that sets it apart from the rest, yet maintains the 
trademark Mercedes design. By no means this sedan is small, and has quite a 
commanding presence on the roads, as all models from Mercedes do!<br>
<br>
Once you step inside the car, it’s a different world all together. The S Class 
emanates luxury from all quarters — be it the wood and leather trimmings inside 
the car, the ergonomically designed and improved Cockpit Management and Data 
System (COMAND) navigation system, or the comfortable yet sturdy driver-specific 
seats.<br>
<br>
Mercedes takes the driver’s and passengers’ comfort seriously, which is evident 
in the multi-contour seats of the S-Class. The seats adapt to your comfort and 
can be adjusted in a number of combinations to suit your liking. Unlike many 
other models, this feature can be experienced by the backseat passengers too. 
And what’s exceptional about these seats is they come with a massage function, 
which can be great during long rides — or, like we realised, when returning home 
after a hard day’s work.<br>
<br>
The improved COMAND in this model from Benz comes packed with innovative 
features. The music (FM, CD and MP3), temperature, navigation, DVD and phone can 
be operated with the convenient ‘push and turn’ controller in the centre 
console. The navigation of these controls is easy thanks to the fairly large 
colour display in the centre of the dash, which also doubles up as your view 
screen while reversing (more on that later). The leather padded hand rest, which 
we found to be quite novel, opened up to reveal a telephone keypad — just in 
case you get the S-Class fitted with a phone.<br>
<br>
Other interesting features in the car include the ambient lighting inside the 
car, a large sunroof and spaciousness that will bowl you over along with 
automatic environment adaptive systems for windscreen wipers, lights and the 
steering wheel.<br>
<br>
<b>Cruising with technology<br>
</b><br>
When it’s a Mercedes you can be assured of comfortable rides even on the 
bumpiest of roads. In the highway traffic it was a smooth operator. But then, 
the Mercedes is also built to handle tougher situations. We decided to challenge 
the S-Class and took it to one of the uneven pathways in Azaiba. True to the 
Mercedes’ word, the S-Class moved along smoothly as if there were no 
irregularities on the road. That’s how an impressive air suspension system 
should perform! Equipped with an Airmatic suspension that keeps the comfort of 
driving on different terrains uniform, the car has been engineered to adapt to 
the different modes it supports — Sports, Manual and Comfort.<br>
<br>
The S-Class has the standard 7G-tronic 7-speed gear, which the manufacturers say 
reduces fuel consumption. This happens as a result of the reduction of the 
average engine speed while shifting gears. The car comes with infrared headlamps 
that illuminate the road ahead yet does not blind oncoming drivers. And if 
that’s not enough visibility, then you can see objects ahead which would 
otherwise not be seen with low-beam bi-Xenon headlamps, which are also 
highlighted by infrared lights on the speedometer display area.<br>
<br>
Night View Assist technology — Mercedes’ novel USP — is the eight-inch 
speedometer display area which transforms into a screen showing the objects 
ahead upto 210 metres, captured through an infrared sensitive camera on the 
inside of the front windscreen.</font></p>
		<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
		<font face="Verdana" size="1"><b>Safetyspeak<br>
		<br>
		</b>
<img border="0" src="safetyspeak.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="200" height="133">The 
		S-Class is Mercedes’ flagship in more than one ways, especially where 
		safety is concerned. Besides the Night View Assist, the S-Class has been 
		incorporated with improved safety features like the Brake Assist PLUS 
		system which comes with two radar systems for early detection and 
		prevention of accident. No wonder studies showed that the heart beat 
		rate of drivers was down by up to six per cent in the S-Class.<br>
		<br>
		PRE-SAFE, the preventive occupant protection system, recognises 
		potential dangerous manouvres and activates related safety systems in 
		the S-Class. Flashing red brake lights warn the drivers behind of sudden 
		braking to prevent tailgating is another S-Class safety feature.<br>
		<br>
		As with some of the models in the same category, the S-Class also 
		features a reverse parking view screen — a boon for motorists who are 
		worried about backing into other cars or poles. Add to that the six 
		sensors — four in the front and two at the back — which warn you of any 
		obstacles in the vicinity of approximately 11 metres, and you can put 
		all your parking fears to rest.<br>
		<br>
		The S-Class is definitely a great vehicle that provides not just the 
		driver but also the passengers some of the best features where comfort, 
		technology and safety are concerned — with good looks to boot. 
		Undoutbtedly, it was the car of our dreams!</font></p>
