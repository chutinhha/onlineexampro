<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<br>
</b><font face="Verdana" style="font-size: 14pt"> <b>Migrating non-profits to 
self-sufficiency</b></font><font face="Verdana" size="1"><b><br>
That’s by driving a “double bottom line” — a virtual blend of financial and 
social returns, learns David Batstone</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"> <b>
<img border="0" src="manag1.jpg" width="100" height="152" align="left" hspace="5" vspace="2">S</b></font><font face="Verdana" size="1">ocial 
innovators around the world have begun to reach a disquieting conclusion: 
inspired vision, impassioned leadership, enthusiastic volunteers, government 
subsidies and a phalanx of donors are not always enough. But there comes a time 
when most founders begin to understand that living from year to year does not 
ensure the future, and that is the moment when they begin migrating from 
innovation to entrepreneurship. They turn toward commercial markets, gradually 
exploring the possibilities for earned income, many for the first time, and 
often with reluctance given their uneasiness about the profit motive.<br>
<br>
The above paragraph is essentially how my friend Jerr Boschee starts his new 
book, Migrating from Innovation to Entrepreneurship: How Non-Profits Are Moving 
Toward Sustainability and Self-Sufficiency. Jerr provides an overview of the 
forces that led to the emergence of social entrepreneurship in the United 
States. As fascinating as that may be, I found his 14 “critical success factors” 
for building a successful social enterprise the most significant part of the 
book.<br>
Jerr’s personal history in many ways tracks the development of social 
entrepreneurship. Perhaps better put, he is both product and pioneer of a social 
movement. In 1968, he worked in north India as a volunteer with the Peace Corps. 
He returned to the United States and eventually landed a position working 
alongside (as personal aide to) Bill Norris, the founder of Controlled Data 
Corporation. CDC was a pioneering supercomputer firm.<br>
</font><font face="Verdana" style="font-size: 11pt"> <b>
<img border="0" src="manag2.jpg" width="250" height="270" align="right" hspace="5" vspace="2"></b></font><font face="Verdana" size="1"><br>
Bill Norris also had a passion for building social enterprises. He felt that the 
Carnegie model of philanthropy — make lots of money, then give it away — put 
limitations on non-profits. Because the non-profit would be dependent on its 
latest donor, it would lack a certain stability. Beyond that, the non-profit 
would have a two-track focus — its primary social mission as independent from 
raising the money to fuel that mission. Wouldn’t it be better, Norris reasoned, 
to integrate the operations more directly into a social mission?<br>
<br>
Norris banded CDC together with other major corporations to create what became 
the National Center for Social Entrepreneurs. Jerr worked there for 16 years, 
serving as President and CEO for the last nine. Today he wears two hats, as 
director of an agency offering volunteer opportunities for former Peace Corps 
volunteers, and as director of the think tank Institute for Social Enterprise.<br>
<br>
In his new book, Jerr shares the lessons he’s learned about driving a “double 
bottom line” — a virtual blend of financial and social returns. He warns that 
the single greatest obstacle in making this shift is the resistance of the 
organisational culture. “The cultural transformation can turn into a war,” he 
notes. Hiring people from the for-profit world often becomes the match in the 
tinder box.<br>
<br>
After giving some hints how to soothe the cultural transition, Jerr turns his 
attention to those factors that are essential ingredients for success. They 
include attitudes like candour and courage, as well as operational thresholds 
like building the right team and strategic marketing.<br>
<br>
Given my own experience in the social venture world, I latched on to his 
critical factor No. 10: “focus, focus, focus.” No, that’s not a misprint; you 
can never get enough of focus in building a social enterprise. As Jerr points 
out, non-profits will be ever tempted to pursue an unrelated business 
proposition that has the allure of being a cash cow. As long as it supports the 
social mission, who cares if it’s off course, right?<br>
<br>
Wrong...most of these efforts fail because it takes the venture into areas where 
they have little expertise or network of partners. Jerr’s advice is to nail a 
commercial niche that is directly connected to the social mission of the 
organisation.<br>
<br>
In many respects, Jerr’s book is a primer for social enterprise. Those already 
in the game have probably learned many of his lessons the hard way. But even 
seasoned social venture entrepreneurs may want to use it in their organisation 
as a training manual to make sure that everyone is on the same page. I love how 
Jerr closes his book, quoting non-profit executive Robert Harrington: “If you 
want to help poor people of the world, step one is to make sure you’re not one 
of them.”<br>
<i><br>
David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States.</i></font></p>

