<b>
Slammin’ 
Sam</b></font><font face="Verdana" size="1"><b><br>
Meet Sam Snead, the greatest golfer of all time with 82 PGA Tour wins — more 
than that of Jack Nicklaus and Tiger Woods</b><br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>
<img border="1" src="golfupdate.jpg" width="192" height="145" align="right" hspace="5" vspace="2"></b></font><font face="Verdana" style="font-size: 11pt"><b>S</b></font><font face="Verdana" size="1">am Snead was the greatest athlete 
among golf’s</font><font face="Verdana" size="1"> great champions. The swing he employed to win tournaments over six 
decades remains the archetype of power and grace, his longevity the benchmark of 
an incredible talent. In more than 50 years as an active competitor, Snead won a 
record 82 official PGA Tour events (more than anybody else till date), and he 
can safely claim more than 140 worldwide. Nicknamed The Slammer for the strength 
of his shots, he won seven major championships.<br>
<br>
Standing at 5 feet 10 inches and weighing a sinewy 84 kg, Snead was classically 
configured and preternaturally gifted. In high school, he could run the 100-yard 
dash in 10 seconds flat. Into his 70s he could, from a dead standstill, kick the 
top of a seven-foot doorway. His movements were almost rhythmical, and it was no 
surprise to anyone that Snead had taught himself to play the banjo and the 
trumpet by ear alone.<br>
<br>
<img border="0" src="golfupdate1.jpg" width="100" height="132" align="left" hspace="5" vspace="2">As 
a boy, who preferred to walk barefoot, he learned golf in much the same way — 
and on those rare occasions when his rhythm was off, he could regain it by 
removing his shoes and socks, as he did for nine holes at the 1942 Masters. Once 
during a discussion of his swing keys, Snead said simply, “I try to feel oily.” 
On another occasion he said, “When I swing at a golf ball right, my mind is 
blank and my body is loose as a goose.”<br>
<br>
Born in 1912, Samuel Jackson Snead was first captivated by golf while watching 
his older brother Homer belt balls across the family’s cow and chicken farm. 
Soon he was fashioning his own clubs out of swamp maple limbs, and using the 
balls he had found caddying at a nearby golf course. He got his first assistant 
pro job at 19 — and in 1936, joined the PGA Tour.<br>
<br>
Immensely long off the tee and a creative shotmaker, Snead took the Tour by 
storm. He won four times in 1937, and eight times in 1938. Under the guidance of 
promoter Fred Corcoran, who marketed Snead as a hillbilly force of nature out of 
the Back Creek Mountains of Virginia, he quickly became golf’s prime attraction. 
“Sam Snead,” said Gene Sarazen, “is the only person who came into the game 
possessing every physical attribute — a sound swing, power, sturdy physique and 
no bad habits.”<br>
<img border="0" src="golfupdate2.jpg" align="right" hspace="5" vspace="2" width="200" height="194"><br>
The next year, he was even hotter, but suffered a major setback at the US Open. 
Thinking he needed a birdie on the 72nd hole to win, but in reality needing only 
a par 5, Snead made an eight to finish tied for fifth. History records that he 
finished second 
in the US Open four times — failing to hit the top spot once when he missed a 
30-inch putt on the final green, during a playoff with Lew Worsham in 1947.<br>
<br>
Still, Snead won three Masters, three PGAs and the British Open in 1946 at St 
Andrews. He led the money list three times, won the Vardon Trophy four times, 
and played on seven Ryder Cup teams. His final major championship victory was 
perhaps his most memorable. At the 1954 Masters, Snead, tied after 72 holes with 
Ben Hogan, defeated him in an 18-hole playoff, 70 to 71.<br>
<br>
Shortputting eventually stopped Snead from winning at his normal pace. Even so, 
his achievements in his later years were remarkable. In 1965, Snead, 52 years 
and 10 months, became the oldest winner of a PGA TOUR event when he won the 
Greater Greensboro Open for the eighth time. He was fourth in the PGA 
Championship at age 60 in 1972, and third in 1974 at age 62.<br>
<br>
“Desire is the most important thing in sport,” said Snead once. “I have it. 
Jeez, no one has more than I’ve got.” <br>
