<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><b><br>
</b><font face="Verdana" style="font-size: 14pt; font-weight: 700">MSM regains 
the 5K mark</font><font face="Verdana" size="1"><b><br>
A smart recovery in tune with the GCC market — but punters watch out for US 
anti-inflationary interest hike</b><br>
<br>
The MSM 30 Index saw movements opposite to last month’s performance, having 
grown by 3.38 per cent over the last 4 weeks (period 21 July – 20 August 2006). 
The three sector indices experienced gains in value as well, while the services 
sector still remains the only sector that has reached a level higher than at the 
start of the year so far (See Table MSM 30 Index). In fact at the time of going 
to the press (day ending 24 August), the market crossed the 5,000 barrier for 
the first time in last two months, closing at 5,035.45 points.<br>
<b><br>
Top Gainers and Losers</b><br>
The five companies that led the table of gainers were Oman Cables Industry (up 
27 per cent), Al Hassan Engineering (up 26 per cent), National Aluminium 
Products (up 14 per cent), Bank Dhofar (up 14 per cent) and Majan Glass (up 13 
per cent).<br>
On the other hand, the five companies whose share value witnessed the biggest 
drops over the past month were Dhofar Insurance (down 21 per cent), Oman Textile 
Mills Holding (down 19 per cent), Oman National Investment Holding Company 
(ONIC) (down 14 per cent), Oman Fisheries (down 11 per cent) and Muscat Finance 
(down 10 per cent).<br>
<b><br>
Volume and Turnover Toppers </b><br>
The most actively traded shares in terms of volumes this month were Alliance 
Housing Bank, BankMuscat, Omantel, Dhofar International Development and 
Investment Holding Company, and Renaissance Services. In terms of turnover 
though, Omantel and BankMuscat topped the charts, being the only two companies 
with traded volumes exceeding RO5 million.<br>
<b><br>
GCC Market</b><br>
Most markets across the GCC region have experienced gains in their respective 
index values — barring the Abu Dhabi Securities Market and the Kuwait Stock 
Exchange, whose indices dropped by 0.58 per cent and 1.28 per cent respectively. 
The Tadawul All Share Index (Saudi Arabia) registered a growth of 6.19 per cent 
over the last month, followed by the Muscat Securities Market (up 3.38 per 
cent), the Bahrain Stock Exchange (up 3.15 per cent), the Dubai Financial Market 
(up 2.09 per cent) and the Doha Securities Market (up 1.43 percent).<br>
<b><br>
Impact of international developments on Oman</b><br>
The US economy currently faces the threat of inflation owing to increasing oil 
prices. This may prompt the Federal Reserve to pump up interest rates. With all 
GCC currencies pegged to the US dollar, an increase in US interest rates will 
need to be followed by a similar effect on GCC interest rates, therefore leading 
to more expensive lending rates for GCC nationals who borrow for the purpose of 
stock market speculation as well as for other industrial and manufacturing 
activities.<br>
<br>
While depreciation in the value of the US dollar can impose higher costs with 
respect to higher prices of imports from non-US trading partners, it can also be 
advantageous towards the promotion of non-oil exports as the GCC currencies 
depreciate in response to any depreciation in the US dollar against other 
floating currencies. The above scenarios will have significant impacts on Oman’s 
economic outlook.</font></p>
