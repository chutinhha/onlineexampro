</font><b><font face="Verdana" size="4">‘Understanding local customer is the 
key’<br>
</font></b><font face="Verdana" size="1"><br>
<b>
<img border="0" src="cover7.jpg" align="left" hspace="10" vspace="4">Dubai-based retailer Landmark Group currently operates in Oman under the 
brands Centrepoint (an amalgamation of four brands viz. Babyshop, Shoe Mart, 
Splash and Lifestyle), Home Centre, Emax and Max. Saibal Basu, GM, Landmark 
Group, talks about the group’s strategies in Oman’s retail market</b><br>
<br>
<b>How will soaring property rents impact growth of the retail industry in Oman?<br>
</b>The shortage in mall space is causing rents to rise as there is a dearth in 
supply. With the current economic boom in Oman and influx of large numbers of 
expatriates there is large rise in retail demand. Major brands would definitely 
like to enter Oman but the shortage in retail space is hindering entry. <br>
<br>
<b>In view of the fact that the leading retailers in Oman are foreign (for 
instance Lulu, MAF, Safeer, Sultan Centre, etc) what is the prospect for a local 
Omani retailer to create a niche?</b><br>
In today’s world, most efficient businesses are global and their success is due 
to their efficiencies and not their nationalities. Any good retailer who can run 
an efficient business and is able to understand the needs of the local customer 
would be able to create a niche for itself. The nationality in this instance 
would be incidental. Oman with its history of large and efficient business 
houses has no reason why a local business would not be able to carve a niche for 
itself.<br>
<br>
<b>In view of the falling rate of exchange of the Rial against major currencies 
recently, how do you ensure price competitiveness of imported merchandise in 
Oman?<br>
</b>Through prudent financial management, the group ensures proper currency 
hedging. Business is largely done in USD which is pegged to the Omani Rial. 
Therefore, we are not much affected by the falling rate of exchange. Moreover, 
due to the economies of scale, a lot of variation is absorbed which helps in 
maintaining price competitiveness.<br>
<br>
<b>What strategies do you adopt to increase footfalls?<br>
</b>We plan the activities for the year, be it a promotion, sale or new arrivals 
from any of the brands, so as to ensure that the customers always have many 
reasons to shop at our stores.</font></p>
