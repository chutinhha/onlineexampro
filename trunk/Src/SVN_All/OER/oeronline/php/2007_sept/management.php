<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Walking the Line</font></b><font face="Verdana" size="1"><br>
<br>
<b>Bettina Buechel discusses the potential pitfalls of creating an effective 
team, especially in the case of new product development</b></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="manage.jpg"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
Nowhere in an organisation is the need for knowledge creation and transfer more 
evident than in the process of new product development, which involves 
conceiving, designing and delivering a new product to the market, along with 
dealing with all the myriad problems that can crop up along the way.<br>
<br>
To do this, a leader has to form a multi-functional team that must understand 
the market, position the product and develop internal capabilities to launch the 
new product. Then, the team has to solve the problem of communicating that 
knowledge to the relevant departments within the organisation, such as sales, 
marketing and production. The question of how to best combine a team’s 
creativity with a capacity to transfer that knowledge to others within the 
organisation is of vital importance.<br>
<br>
On paper it looks straightforward, but a deeper look at what goes on reveals 
several contradictions a successful team leader needs to be able to resolve. 
Within a team, there needs to be the complicity, trust and instinctive 
understanding that comes from team members working together. <br>
<br>
Junior members of the team, for example, must feel free to contribute their new 
ideas without intimidation. It takes time to develop a closely-knit team that 
will share information rapidly and will be motivated to contribute its knowledge 
to the project, knowing that there will be recognition from the group. The 
sharing of information among the team is known as “social capital.”<br>
<br>
<b>Keep creativity alive</b><br>
However, a closed network has both advantages and drawbacks, which a leader 
needs to consider when evaluating the effectiveness of the team. Many teams go 
through a typical lifespan. In the beginning, a lot of work needs to be done 
setting up the group and fostering communication channels and norms of behaviour, 
but the resulting closely knit group runs the risk of falling into the trap of 
groupthink: The more members work together, the more likely their individual 
views are to merge, mutually supporting each other and resisting ideas that 
challenge the team’s common beliefs, blocking its original creativity and 
innovation.<br>
<br>
As a team develops its own thinking and communication, it can lose touch with 
the outside. A team developing a new product, for instance, may become so locked 
into its own thinking that it becomes difficult for an outsider to understand 
what the team is doing. <br>
<br>
It is therefore vital to keep the channels of communication open between the 
team and the outside to ensure that knowledge is transferred from the team to 
the outside, and that the team continues to receive and be receptive to the flow 
of knowledge from the exterior.<br>
<br>
<b>Maintain outside contact</b><br>
The trick is to be able to form a closely-knit team that will work well together 
with the knowledge brought in by external individuals. Some members can serve a 
bridging role-ensuring that all members of the team have access to vital contact 
from the outside that will alert the team to important new information.<br>
<br>
This, combined with the team information, will create new knowledge. This 
prevents the team from converging its views and resisting the integration of 
alternative information. The more contact central members of the team have 
outside the group, the greater the knowledge created and the more social capital 
it develops. This leads to better performance because external influences will 
challenge the team and force it to re-evaluate its knowledge according to new 
perspectives.<br>
<br>
A team is at its best when it has strong cohesive bonds within the group and is 
at the same time open to external influences (and is thus willing to incorporate 
new ideas into its thinking).<br>
<br>
A skilled team leader will have a vital role to play: ensuring that the team 
gets the feedback it needs to continue building on team cohesiveness, while 
developing its exterior contacts, which the team will also need to implement its 
thinking. This offsets team-building with the need to inject constant 
stimulation from the outside, both to prevent the team becoming embedded in its 
own beliefs and to enable the group to communicate with its future customers.<br>
<br>
