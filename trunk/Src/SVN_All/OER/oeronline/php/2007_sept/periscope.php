<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Bottom Picking in Emerging Markets<br>
</font></b><font face="Verdana" size="1">
<br>
<b>
There is currently a traumatic re-pricing of risk in the global markets and 
emerging markets equity will have to re-price itself down, writes Matein Khalid<br>
</b><br>
<b>
<img border="0" src="periscope.jpg" width="157" height="240" align="left" hspace="10" vspace="3"></b>There is an existential problem in bottom fishing. Sadly, it does not ring a 
bell to announce a stock market bottom. Thus, it was inevitable that the 
emerging markets would suffer from the fallout of the credit spasm on Wall 
Street. Logic dictates that there is no reason why Qatar, Egypt or Turkey should 
pay a higher spread to US Treasuries when they float sovereign bonds in the 
Eurobond market just because borrower Joe and Jane in the US went ballistic on 
their sub-prime mortgages and the ‘Masters of the Universe’ in Wall Street 
private equity hoped to sell $300 billion in leveraged buyout loans to 
international money centre banks. But financial markets are not driven by logic, 
instead by leverage and psychology, particularly during moments of fear and 
crisis. As in 1998, there is currently a traumatic re-pricing of risk in the 
global markets and emerging markets equity will have to re-price itself down. 
There is 20-30 per cent risk in the world’s major emerging market indices, in my 
view. Here’s how.<br>
<br>
<b>Assessing risk<br>
</b>One, a hard landing in the US is almost inevitable. The July non-farm 
payroll numbers prove that layoffs in construction, trucking, rail, 
manufacturing and services have now begun. Sale taxes receipts and auto sales 
have fallen in Florida and California. These metrics are classic proxies for 
consumption, which was also goosed by consumers using home equity as their ATM 
cash gusher. Moreover, ISM manufacturing orders have fallen and the unemployment 
rate could well rise to 5 per cent by Christmas. Financial distress has swept 
Wall Street, with dozens of hedge funds and mortgage banks on the brink of 
failure.<br>
<br>
A hawkish Fed, with the funds rate at 5.25 per cent, is totally surreal at a 
time when the economy is drifting into recession. The Fed has no choice but to 
execute a monetary policy U-turn, exactly as it did in October 1998 and December 
2000. The world’s bond markets have gone into systemic crisis. The sub-prime 
crisis has infected the entire financial complex, and hedge funds from Australia 
to Germany are in trouble, let alone the trillion-dollar CDO time bomb in 
international banking balance sheets. There is no way the Fed can risk a 
monetary Armageddon and a credit risk freeze across the global financial 
markets. In such moments, central banks do not manage inflation expectations or 
asset prices, they pump credit into their banking systems, act as lenders of the 
last resort. My call? The FOMC cuts the Fed Funds rate by 50 basis points on 
October 31, as Ben Bernanke acts as the white knight of global finance. 
Short-term interest rates plunge, as in 1992, 1998 and 2000 when the Fed cut 
interest rates in response to financial distress. My call? The Fed Funds rate 
falls to 4 per cent by the summer of 2008 as the US flirts with economic 
recession.<br>
<br>
<b>Asian markets<br>
</b>There is no doubt that Asia cannot decouple itself from the credit market 
distress on Wall Street, let alone a consumer recession in the US. Hedge funds 
are the agents of global contagion, exactly as was seen in the fateful summer of 
1998 when the Thai baht tanked, South Korea went bust and needed a jumbo IMF 
bailout, and Indonesia’s financial collapse led to the end of the 32-year-old 
military regime of General Suharto. True, Asia is not leveraged to its eyeballs, 
as it was in 1998. On the contrary, Japan, China, Taiwan, India, Singapore and 
South Korea own more than US$2 trillion and, ironically, are America’s most 
important collective creditors. The LBO, the CDO, the CLO and the corporate junk 
bond is virtually unknown in Asia, given its small, shallow corporate debt 
market and conservative, leverage shy bankers.<br>
<br>
Yet, some Asian markets are still expensive. Indonesia, Hong Kong, Singapore and 
Malaysia are all exposed to a US recession and a spike in risk aversion / 
volatility / emerging markets credit spreads but trade at high valuations since 
the 1980s. After all, during the credit crunch cycle of the early 1990s, Hong 
Kong lost half its market capitalisation in three years. It is also ominous that 
Asian central banks such as China’s Peoples Bank, India’s Reserve Bank, and 
South Korea’s BOK are determined to raise interest rates to cut bank credit 
growth. The telltale signs of global contagion have now hit Asia. Fund flows 
into Asian mutual funds have plummeted, the Chicago volatility index has doubled 
in a mouth and Asian financials as diverse as Kookmin Bank and Reliance Capital 
have lost 10 per cent of their market cap in a week. Asian emerging markets have 
no value now. I also expect even a hint of a US recession will lead to a 
bloodbath in the commodity market. This is the time to go short in energy, metal 
and mining shares. Sector rotation also argues for a switch from exporters to 
domestic demand shares or large cap tech, whose valuations and growth are fine 
now. High beta markets vulnerable to speculative risk appetites, such as Turkey, 
Brazil and India, are vulnerable to an EM blowout. There is a time for greed and 
a time for fear. This is now the time for fear. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
