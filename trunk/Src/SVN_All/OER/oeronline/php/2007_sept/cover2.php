</font><font face="Verdana" size="4">‘Retailers need to pull up their socks’<br>
</font><font face="Verdana" size="1"><br>
<b>
<img border="0" src="cover8.jpg" width="181" height="195" align="left" hspace="12" vspace="4">Retail International, a UK based consultancy, was founded in 1993 to advise 
shopping centre developers and retailers with a particular emphasis on the 
Middle East and North Africa. Simon Thomson, its Principal, has been involved 
with shopping centres in the region for 35 years and was a founder of the Middle 
East Council of Shopping Centres, Dubai. In an exclusive interview with OER, he 
shares his insights on Oman’s retail sector scenario. Excerpts:<br>
</b><br>
<b>What is driving the retail sector boom in Oman?<br>
</b>The main drivers are the improving economy caused by the continuing high 
prices for oil and gas plus a rapidly growing indigenous population. This means 
that there is a rapidly growing, affluent and young population–all the right 
demographic ingredients to make a retailer happy. The key driver however is the 
local population with the expats and tourists providing ‘the cream on the top’. 
Reliance by any retailer solely on expats or tourists would be a high risk 
strategy other than for those specifically targeting that precise sector of the 
market.<br>
<br>
<b>What’s the retail model being adopted in Oman?<br>
</b>I would suggest following very much the pattern from elsewhere in the GCC, 
with shopping malls combining a mix of international brands by way of franchise 
plus strong local brands, anchored by a major space user, typically a 
hypermarket or one or more department stores. In malls, the practice normally is 
for the retailer to rent for say 3, 5 or more years. Freehold ownership in 
shopping malls is unusual. Franchise is the preferred and now well established 
route for retailers to bring international brands to the Middle East, including 
Oman. Historically this was because 100 per cent foreign ownership was not 
possible and in some Gulf countries this is still the case.<br>
<br>
<b>How would the coming of hypermarkets and malls impact the traditional small 
retailers in Oman?</b><br>
In terms of the corner store or souk trader possibly not as much as might be 
feared because small independent shops, provided they are adaptable, will always 
find a niche market based on personal, friendly and knowledgeable service that 
impersonal self-service hypermarkets can never provide. The arrival of 
hypermarkets and modern malls have had a beneficial effect for shoppers in that 
they have introduced greater competition, longer opening hours and up-to-date 
stock.<br>
<br>
<b>In what ways should Oman’s retail businesses adapt themselves to meet the 
challenge of increased competition and higher customer expectations?<br>
</b>Generally, by pulling up their socks. It’s a harsh business where only those 
willing and capable of adapting to changing conditions will survive. Retailers 
have to be pro-active in generating customers–in the past it was too easy to sit 
back and do nothing in the knowledge that shoppers had nowhere else to go. This 
is no longer the case. Also the possibility of being able to shop online 
provides a further element of competition that ‘bricks and mortar’ retailers 
ignore at their peril–even if it is only for price comparison.</font></p>
