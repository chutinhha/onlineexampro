<p align="justify">
<b><font face="Verdana" size="4"><br>
</font></b><b><font face="Verdana" size="4">Looking to Port</font></b><font face="Verdana" size="1"><br>
<br>
<b>Oman’s ports are increasingly being seen not only as conduits but as centres 
for industry and logistics, which will draw in people and the services they 
need, writes Joseph Smart</b><br>
<br>
<b>
<img border="0" src="economy.jpg" width="160" height="453" align="left" hspace="10" vspace="4"></b>Oman’s location on the Indian Ocean side of the Arabian peninsula has created a 
seafaring culture and a trade-oriented economy that remains to this day. The 
importance of the country’s ports has been enhanced by exports of oil, which 
form the backbone of the Omani economy. As the government promotes 
diversification, it envisages that the role of ports will be deepened as drivers 
of the economy with the core of economic zones thriving from the port’s 
activities. While the country’s two big ports continue to develop and see 
increased traffic, the newer Sohar Port is being turned into a manufacturing and 
logistics hub that could punch at regional weight, or above.<br>
<br>
The first five months of this year saw the volume of cargo handled by Oman’s two 
largest ports increase by 31 per cent. Mina Sultan Qaboos and Salalah Ports 
handled between them 2.852 million tonnes compared to 2.178 million tonnes in 
the same period of last year. The volume of landed and unloaded goods at Mina 
Sultan Qaboos in Muscat increased 41.9 per cent to 1.759 million tonnes from 
1.239 million tonnes between January and May last year. Meanwhile, Salalah Port 
handled 1.094 million tonnes of cargo, up 16.5 per cent from 938,000 tonnes in 
the same period last year. The ports handle goods, including stuffs, vehicles 
and materials for the country’s growing construction sector. <br>
<br>
According to the Ministry of National Economy, 1,045 ships called in at the 
ports by the end of May, down 8 per cent from the first five months of 2006. 
Salalah saw the sharpest fall in ship anchorage, down 21.2 per cent to 217, 
while at Mina Sultan Qaboos, 774 ships docked, a drop of 2.6 per cent from 792 
in the same period last year.<br>
<br>
<b>Strategic location</b><br>
Oman is taking advantage of its strategic location on the straits of Hormuz and 
the Arabian Sea, as well as its long seafaring tradition, to develop its 
shipping sector. For both the government and the private sector, shipping is key 
to diversifying the Sultanate’s oil-dominated economy.<br>
<br>
Mina Sultan Qaboos, inaugurated 1974, serves as Oman’s primary port, with 
Salalah and the Sohar Port acting as industrial and transhipment hubs. Over the 
past decade, the infrastructure at Mina Sultan Qaboos has been improved and the 
port upgraded to take in containerised cargo. The city has become an 
increasingly popular cruise destination, and a cruise ship port is due to be 
completed by the end of the third quarter of this year.</font></p>
<p align="center">
<img border="0" src="economy1.jpg" width="350" height="221"></p>
<p align="justify">
<font face="Verdana" size="1">Salalah Port was opened in 1998 at Mina Raysut, previously a small fishing port 
on a naturally deep-water coastline, with the express aim of becoming a 
world-class container terminal, promoting Oman as a global shipping hub and 
giving a lift to the national economy. It is the largest port in the Sultanate 
and can dock ships with drafts of up to 16 metres. The port can handle liquid 
and bulk cargoes, and has facilities for repairing ships and containers and a 
container freight station.<br>
<br>
The port management company, Salalah Port Services Company (SPS) is 70 per cent 
owned collectively by the government and Omani companies and investors, with the 
remaining share owned by Denmark’s A.P. Moller-Maersk Group, representing the 
largest privatisation project in the country. Currently, SPS is investing US$356 
million to boost the port’s capacity to 4.4 million tonnes per annum and to 
increase maximum draft to 18 metres. <br>
<br>
<b>Economic hub</b><br>
The country’s third port, the Sohar Port, is being developed as the heart of an 
economic region and an engine for economic growth. The city around it is 
expected to develop into a major manufacturing and logistics centre.<br>
<br>
Sohar, 240 km north-west of Muscat, was established in 2002, and its port is a 
50-50 joint venture between the government and the Netherlands’ Port of 
Rotterdam; it docked 335 vessels last year, as it is still under development. 
The port’s management has a pared-down role, with infrastructure, industrial 
production, cargo handling and some other operations run by private companies, 
who provide more expertise and competitive prices. The management company 
concentrates on health, safety, security and the ports’ rules and regulations. 
The Sohar Port authorities emphasise its position in the Abu Dhabi-Dubai-Muscat 
triangle, and just outside the Strait of Hormuz, through which a large 
proportion of the world’s shipped oil and gas passes.<br>
<br>
By 2008, a total of $12 billion will have been invested in the port; it has 
container-handling capacity of 800,000 tonnes per annum, which is currently 
being expanded to over 2.1 million tonnes, and the number of berths is being 
doubled from 12 to 24. The project is scheduled to be completed by October 2008. 
This will be followed by the development of the industrial area and the 
greenfield construction of an industrial complex, which is set to be one of the 
biggest in the Middle East. The authorities see these opportunities as important 
steps towards the diversification targets enshrined in the Oman Vision 2020 
programme. The port is already bringing in raw materials for manufacturers, and 
then exporting their finished products.<br>
<br>
Industries based in Sohar include gas and petrochemical based units 
manufacturing formaldehyde, polyethylene, methanol, amongst other products, in a 
“petrochemicals cluster”. There is also a “metal cluster” where steel and 
aluminium are produced. International firms in the port include Shadeed Iron &amp; 
Steel, Air Liquide, Larsen &amp; Toubro, LG Alcan, Hutchison Port Holdings, Steinweg, 
MAN Ferrostaal, Methanol Holding International, Odfjell and Suez.<br>
<br>
<b>Free zones</b><br>
The clusters aim to improve economies of scale, and therefore profitability, by 
allowing firms with similar requirements to share infrastructure. The Salalah 
Free Zone (SFZ), currently under development, should also help promote trade and 
industry in the region; the zone will cover 2,000 hectares and will offer 
industrial, retail, logistics, freight forwarding, residential, leisure and 
research and development space. Within the SFZ, imports are duty-exempt, a 
generous tax holiday is allowed and foreign companies can own 100 per cent of 
their investment projects. When complete, it is estimated that the port will 
provide 8,000 jobs directly and 30,000 indirectly.<br>
<br>
The development of the SFZ has not been smooth and was originally planned as a 
joint venture with Salalah Port Services and Hillwood Development, which pulled 
out of the project dramatically in 2003 because of differences of opinion. In 
2004, the Public Enterprises of Industrial Estates put forward the idea of 
having the SFZ as a 100 per cent government -owned company with the objective 
that this would give confidence to investors.<br>
<br>
If the SFZ becomes a success, as similar zones have in the region, the model 
could be copied in other parts of Oman and Sohar has been mentioned as a 
candidate. The Port of Sohar is an industrial area with a focus on energy, 
rather than a free zone. It is only 250 km away from the Jebel Ali Free Trade 
Zone in the UAE, which has trading and manufacturing operations as well as 
regional offices for international companies. However, with other ports on the 
Arabian Sea coast of the UAE also beginning to show strong growth, such as Khor 
Fakkan and Fujairah, Sohar needs to act fast to ensure it can build up a good 
list of regular trading partners. Also of concern is the road infrastructure 
between Oman and the UAE. As the UAE may well be a source of both imports and 
exports through Sohar, ensuring the roads between the two can cope with the 
added trucking demand will be key.<br>
<br>
Smaller ports are also being developed for more specific uses, for example Mina 
al-Fahal, which is used for oil exports, and Qalhat, which focuses on liquid 
natural gas (LNG). One branch of Oman’s ports strategy is the government-owned 
Oman Shipping Company (OSC), which was established in 2003 and works under the 
aegis of the Ministry of Finance and Oman Oil Company. OSC has been benefiting 
from the global rise of LNG, and has six vessels equipped to carry gas, with a 
combined capacity of 11.8mn m3; it is now diversifying by purchasing a very 
large crude carrier and a very large gas carrier.<br>
<br>
Oman’s geography and oil resources mean that its ports have been and continue to 
be vital to the economy. Increasingly, however, they are being seen not only as 
conduits, but as centres for industry and logistics, which will draw in people, 
and the services they need. Ports are taking centre stage again, and Oman’s 
position between Asian and Africa, outside of the Straits of Hormuz, can well be 
leveraged into something more important. <br>
<br>
