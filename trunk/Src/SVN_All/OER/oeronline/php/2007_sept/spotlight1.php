<b>Green machines</b>: Carbon dioxide emissions, restriction of hazardous chemicals, 
and energy conservation are also turning into serious trend dictating which 
office equipment manufacturer can exist in future. These are environmental 
issues and Kyoto Protocol is exerting tremendous pressure on manufacturers. Over 
the years Ricoh has led the industry with major innovations in the environment 
related areas. <br>
<br>
Ricoh devices are free from any chemicals as per EU directives on restriction of 
hazardous substances. Most other manufacturers are yet to comply and will not be 
allowed to sell their devices in Europe which can affect their very existence. 
Energy conservation is achieved by the QSU and Standby modes to ensure lowest 
power consumption (just 10watts in standby mode – a significant figure as 
photocopiers are in standby mode for 80% of their life!) Carbon dioxide 
emissions are within new and stricter norms due to special toners used.<br>
