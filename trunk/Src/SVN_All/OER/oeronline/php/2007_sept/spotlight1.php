they have kept pace with technological breakthroughs to incorporate remote 
diagnostics, multifunctional capabilities, paperless faxing, internet and IP 
faxing as in the FAX 4420NF. Machines addressing the entire market segments are 
available .Most prominent success is from the high end heavy duty models.<br>
