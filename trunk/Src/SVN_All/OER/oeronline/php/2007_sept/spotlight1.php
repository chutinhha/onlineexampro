by offering comprehensive solutions (as against box selling approach). Muscat 
Electronics is geared to provide solutions to meet diverse needs of our 
customers. The success of the solutions approach is already reflected in 
increased market share for color and b/w multifunction digital laser copier 
products at a healthy (estimated) 23% market share for RICOH. The ‘full power of 
Ricoh’ due to its enhanced product offering will be even more visible this year. 
Ricoh color laser printers are also gathering market shares rapidly at the 
high-end. Ricoh has an extensive lineup of products designed to efficiently 
create and share documents. The products can help users streamline their 
document workflows and cut costs.<br>
<br>
<b>New Printing technology</b>: The Gelsprinter™ uses the Ricoh patented gel printing 
technology. The printers use fast drying Liquid Gel™, wide print head, advanced 
paper handling technology, to provide vibrant color prints on regular paper at a 
very low cost per printed page at fast speeds even when printing duplex. The 
high speed and color prints that are virtually at the same cost as black &amp; 
white, make these innovative devices an ideal office printer.<br>
<br>
<b>
<img border="0" src="spot5.jpg" width="200" height="191" align="right" hspace="10" vspace="4">RICOH MP C 1500SP</b>: With this product introduction, even the entry level copiers 
will be able to provide color copy and prints and color prints will virtually be 
at the same low cost as black and white. <br>
<br>
<b>RICOH MP C 2500/3500/4500</b>: Machines that offer the economy of black &amp; white and 
the power of high speed colour. Available with various finishing options(such as 
booklet finisher) , these devices are supplied with free software to manage all 
office documents(fax/copy/scan and print data).Document distribution is also 
very efficient due to scan to email and scan to folder features. LAN fax and 
internet fax are cost saving functions on these versatile devices. As they sit 
on the network, IT administrators are supplied with software tools to monitor 
them with web browsers. Security features prevent unauthorized access. Finally 
the cost per copy/print is low resulting in a low Total cost of ownership (TCO).<br>
<br>
<b>RICOH AF MP5500/6500/7500 and AF MP9000/1100/1350</b>: These devices are the heavy 
duty black and white machines with 1200 x 1200 dpi copy/print capability at high 
speeds upto 135 cpm and are the only machines in the industry designed for 
document production in the print shop kind of environment. The machines can 
operate virtually nonstop as toners and paper can be inserted without 
interrupting the machine. The machines are capable of handling tremendous 
volumes (1 million per month). Many reputed organizations have switched to these 
revolutionary machines.<br>
<br>
<b>High speed Color Multifunctional AFICIO 3260/5560</b>: These units are color capable 
at upto 55 pages per minute for fast color reproduction applications.<br>
<br>
<b>Print-on-demand and Data processing environments</b>: Designed to meet the most 
rigorous production printing requirements, Ricoh’s robust engines deliver 
mission critical performance and reliability. <br>
<br>
<b>Wide format Digital copiers (A0 size)</b>: This segment of copiers has undergone a 
sea change with the introduction of Ricoh digital models AF 240W and the AF 480W 
.These models occupy lowest space, operate at low cost per copy/print, 
distribute large sized documents efficiently, scan once for mutiple copies, easy 
to upgrade , connect and customize ,conform to industry standards. Additionally 
it is possible to direct scan large drawings to server PC without loading the 
network and also edit the scanned drawings. Drawings in DWG format can be 
viewed, archived or even converted to TIFF format without the having Autocad 
software! The built in document server makes it even more convenient as it is a 
strong document management repository on the machine itself.<br>
<br>
<b>Scanners</b>: Document management requires high speed heavy duty scanners in order 
to convert existing paper records to electronic format. Color capable scanners 
that scan to print, scan to email, scan to folder and scan to FTP provide 
virtually any document distribution method at the touch of a key. Models IS 
760D, IS 300E, IS 200E and IS 330 are the frontrunners in the race currently in 
the market. These devices can be connected to compatible Aficio laser printers 
as an economical alternative to basic MFP functionality.<br>
<br>
<b>Laser Printers</b>: Ricoh has in its stable some exciting color and black &amp; white 
printers. High speed A3 color printer CL7200 and SPC 811DN and the SPC 
410DN/411DN (A4) have become the preferred models due to speed and print quality 
that is available at a low cost per print. AP 4100N/AP4110N and AP 610N are 
trouble free black and white A4/A3 printers with low TCO. At the high end of 
black and white printing the 45 ppm (AP 8100DN) and 75 ppm (AP 9100DN) models 
top up the wide range for all user requirements.<br>
<br>
