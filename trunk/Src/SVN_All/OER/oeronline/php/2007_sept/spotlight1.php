the security paradox – on one hand there is the inherent advantage of faster 
info sharing and distribution and on the other is the possibility of leakage due 
to the sharing &amp; distribution process. RICOH products offer unique solutions – 
such as printing ‘patterns’ on the pages that when copied result in a blank 
sheet output from the machine. <br>
