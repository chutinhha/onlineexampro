<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">
Covering against risk<br>
</font><font face="Verdana" size="1">
&nbsp;</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1">
<b>Cyclone Gonu has not only changed risk perceptions but also forced a change 
in strategies on part of insurance providers. OER checks out the plans of AXA 
Insurance<br>
</b><br>
If an insurance agent had asked us some three months ago to take out cover for 
storm, tempest and flood (STF), we would retorted why waste money. Storm and 
floods in Oman? No way. But when Cyclone Gonu struck, it not only changed the 
way that people looked at insurance but also the way the insurance companies 
undertook their operations, specially when it came to motor insurance.</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="corp.jpg" width="450" height="322"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
“Gonu has definitely changed market dynamics. Clients are now aware what STF 
means in motor comprehensive insurance and are examining their policies to be 
sure that they have such cover. Insurance premiums are increasing, but not 
enough due to competition and the desire of insurers to capture/ increase their 
market share,” says Deepak Kamath, area manager, Oman, AXA Insurance (Gulf). In 
its aftermath, Gonu has also thrown up several challenges.<br>
<br>
The immediate challenge, post-Gonu, is to settle claims, which run into millions 
of riyals for each insurer. Whilst most of the individual claims for motor, home 
and travel have been settled, the daunting task of disposing vehicle salvages is 
not yet over. The larger corporate claims are now getting finalised and these 
are big ticket payments. “We expect that most corporate claims will be settled 
by September-end,” says Kamath.<br>
<br>
The longer-term challenge for AXA Insurance is self-discipline to insure on a 
sound underwriting basis and charge adequate premiums. Reinsurers have suffered 
the most and hence will raise prices and impose tougher conditions on insurers, 
which inevitably will be passed on to the direct customer. With the looming 
threat of more storms and floods in future due to changing weather patterns, it 
is going to be difficult for all concerned.<br>
<br>
<b>Post-Gonu strategy</b><br>
So what will be the company’s strategy post-Gonu? “AXA’s strategy has not 
deviated. We want to be the preferred ‘employer’ and the preferred ‘partner’ and 
are working towards these objectives. Our ambition is to be a market leader and 
we are on target to achieve this. We believe in offering good products, with 
easy to understand language, advise clients appropriately, deliver excellent 
service and charge a fair price,” asserts Kamath.<br>
<br>
“Basically, in the GCC countries there is low penetration of insurance for both 
life and general products. This is an exciting market with its unique blend of 
emerging market status and a well-developed infrastructure – an ideal mix for 
any service provider. AXA Gulf’s strategy is to increase awareness of the need 
for insurance and help the public at large to adequately protect their families 
and assets. We see a great future for AXA,” affirms Andrea Rossi, CEO, AXA 
Insurance Gulf &amp; Middle East.<br>
<br>
On its part, AXA is not new to the region-it has been here for over 50 years 
though under different names- Norwich Union, Guardian Royal Exchange, AXA 
Insurance Plc before the merger with Norwich Union Middle East led to the 
creation of AXA Insurance (Gulf). Even in Oman, it can trace its roots back to 
1971.<br>
<br>
“Merging with an insurance business that had been operating in the region since 
the 1950s has enabled AXA Gulf to achieve a critical size and become the largest 
international insurer in the region. This enabled it to inherit some strengths 
from Norwich Union, including local expertise and knowledge, intermediary 
relationships, personal and corporate lines of business, marine cargo and a 
spread of offices throughout the GCC. AXA Gulf also gained as its JV partner, 
the Kanoo Family, a respected and established trading family in the region,” 
says Rossi. “AXA’s existing healthcare business, motor and travel portfolios, 
together with the AXA Group support, have contributed to make AXA Gulf a 
formidable force in the market place,” he emphasises. </font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="corp1.jpg" width="450" height="364"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>Global resources</b><br>
AXA Gulf wants to draw upon specialised support functions from the Group’s 
world-wide resources, such as: AXA Cessions, the group’s reinsurance division; 
AXA Way, a Six-Sigma concept to improve internal processes to deliver on 
customer expectations; AXA Corporate Solutions, a specialist in large risks; AXA 
Assistance, a service company for medical and travel claims abroad; and AXA Art, 
expertise in fine art and exhibition risks. The AXA Group Worldwide is a global 
leader in insurance and investments, and among the top 15 in the Fortune 500 
list.<br>
<br>
The company is growing fast to keep pace with the accelerated development in 
Oman and the rest of the GCC, and expects turnover to exceed US$300 million in 
premium income by the end of this year-reflecting a compounded growth rate of 
over 35 per cent in the last three years. AXA wrote approximately $200 million 
in premium, profits were approximately $26 million in 2006. It has a market 
share of about 6 per cent in the Gulf and is aiming to push that to 10 per cent 
by the year 2012 as part of its overall ambition to become the preferred company 
in the Gulf for financial protection.<br>
<br>
“As part of this, we will invest in people and processes. The company already 
employs over 60 per cent Omani nationals and we are planning to further develop 
the talent that the local market has to offer. We put a lot of emphasis on 
training and with the help of AXA Way to improve existing processes and we are 
aiming to meet customer expectations,” avers Rossi.<br>
<br>
<b>Expansion plans<br>
</b>The company’s strategy includes inorganic growth too. “Yes, we’re planning 
to invest further by taking part in other insurance companies, but not by buying 
shares. We prefer full acquisitions and are actively looking within the Middle 
East, specially now that we have the expertise of merging successfully. We may 
look at a local company in the UAE or a regional player, particularly if they 
have a spread of offices in the UAE, Qatar, Bahrain and Oman. However, our 
priority now is to concentrate on organic growth,” reveals the CEO. The company 
plans to expand its operations to all parts of the region. “We received our 
Qatar insurance licence in November 2006 and have now commenced operations. We 
are also in the process of establishing a new company in Saudi Arabia, in 
cooperation with the Saudi Arabian Monetary Agency, in which we will offer 
shares to the public,” Rossi adds.<br>
<br>
Within Oman, the company foresees greater consolidation taking place. “It is 
difficult to service a larger capital with low premiums due to many players and 
hence we could see some mergers as we go forward. We have reassessed our 
position and had decided our strategies before Cyclone Gonu, with very minor 
changes after Gonu. And, we are on track. I am sure that other companies will 
decide their own strategies, specially that there are not many mega 
constructions projects this year as compared to 2006. However, we still expect a 
good growth for the insurance sector in the coming years,” says Kamath.<br>
<br>
With the market getting overcrowded day-by-day, it will be interesting to see 
how AXA Insurance will cover itself for the future. <br>
