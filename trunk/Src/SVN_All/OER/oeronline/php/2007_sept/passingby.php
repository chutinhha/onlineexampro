<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Looking for entrepreneurs<br>
</font></b><font face="Verdana" size="1"><br>
<b>Dr Gottfried Neuhaus of venture capital firm Neuhaus Partners GMBH was 
recently in Muscat for the launch of Oman Venture Capital Fund. Akshay Bhatnagar 
spoke to him on the sidelines of the event<br>
</b><br>
<img border="0" src="passingby.jpg" width="250" height="188" align="left" hspace="10" vspace="4">Despite the investment sector growing at a very healthy rate, Oman has never had 
a professional venture fund to support and develop budding entrepreneurs. Though 
Knowledge Oasis Muscat (KOM) has been instrumental in incubating new 
entrepreneurs, there has been a genuine need to take those enterprises to the 
next level. To fill the void, Oman Venture Capital Fund has been created by 
Omani Center for Investment Promotion and Export Development (OCIPED) with the 
support of Hamburg (Germany) based Neuhaus Partners GMBH.<br>
<br>
Dr Gottfried Neuhaus, the managing partner of Neuhaus Partners, spent a couple 
of days in Oman to announce the launch of the fund and measure the market 
potential for it. Speaking to OER, he informed: “We are a venture capital 
financing firm that supports growth companies from sectors like the IT 
(hardware, software and services), Internet (infrastructure and services), 
telecommunications (services and technology), laser and photonics, microsystems 
technology and multimedia with technological know-how, comprehensive consulting 
and equity capital from founding to IPO and beyond with the objective of 
continuously creating added value for our companies. We have successfully raised 
two funds before – EUR17 million in 1998 and EUR52 million in 2000. We are in 
the final closing of Fund III with EUR100-million capital.”<br>
<br>
On the Omani venture, he said, “Neuhaus Oman Venture Capital Fund will have a 
first closing of EUR50 million (RO26 million approximately) and final closing of 
EUR100 million (RO52 million approximately). The entire corpus will be raised in 
Oman and is expected to be driven primarily by banks, pension funds, high net 
worth individuals (HNIs) and other entities. We will be investing in early stage 
technology companies in Europe and GCC region. Up to EUR20 million (RO10.4 
million approximately) investment will be marked for GCC region with a special 
focus on Oman.” It will be interesting to see how many companies or HNIs show 
interest in investing in the fund as the minimum subscription is for EUR5 
million (RO2.6 million approximately). Alliance Housing Bank and Omani Youth 
Fund are expected to invest in the fund.<br>
<br>
“We are planning to start operations after Ramadan and expect to raise the money 
by the end of this year. I’m confident that we will raise the money successfully 
in the desired time,” said Dr Neuhaus. What if the fund fails to generate the 
minimum EUR50 million? “Then there is no point in carrying on as it does not 
make any sense to operate with an amount lower than EUR50 million,” he said.<br>
<br>
What is the minimum disbursement per enterprise that the fund will be aiming at? 
He answered, “In Europe, we invest in the range of EUR1-5 million. But as the 
market structure in Oman is different we are looking at starting from 
EUR500,000.”<br>
<br>
Neuhaus Partners’ operations in Muscat mark their entry in the GCC region. Why 
Oman? “I see a lot of action taking place in Oman and I think there is a 
definite need of a fund like ours. Moreover, OCIPED has given us great support 
in launching the operations and raise the funds. My question is why not Oman?”<br>
<br>
He added, “Three-to-four decades ago, entrepreneurs believed in building the 
business to pass on to their future generations. But now the thinking has 
changed. Entrepreneurs are in a hurry to build and expand their business fast 
and opt for raising finance from outside to put up an enterprise with a larger 
scale to reap a good valuation. They sell it partly or completely to make good 
money for themselves instead of passing it on to their next generations. I see a 
similar trend sweeping the region, which means we have a major role to play 
here.” </font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1">
