<font face="Verdana" size="1">AL NAHDA RESORT &amp; SPA <br>
</font><font face="Verdana" size="4">Trip to tranquility</font></b></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<img border="0" src="leisure.jpg" align="left" hspace="10" vspace="4">Situated 
just off the Muscat-Sohar highway, 30-minutes from the Seeb International 
Airport, Al Nahda is more than just a resort spa. It is a whole holiday 
experience which will give you the tools to improve your life balance. The place 
is full of opportunities to learn new life skills and what could be your first 
step on the road to a fitter, healthier and more balanced lifestyle. It can help 
you get fitter; manage your weight; check your Aura and Charka balance; learn 
yoga, meditation or a new sport. On arrival, the Life Style Consultants will 
help you plan your stay. Your treatments and classes are programmed as per your 
requirement to include time out to go shopping, diving or some sight seeing. The 
retreats makes you relax, rejuvenate, de-stress, detoxify and shape-up. The Spa 
villas offer a blend of traditional and new therapies which are designed to 
promote better balance between mind, body and spirit. The holistic health is 
provided by a team of naturopaths and specialists who are dedicated to providing 
you with the know-how so you can embark on the road to a better lifestyle.<br>
<br>
<img border="0" src="leisure1.jpg" align="right" hspace="10">Whether 
a beginner or a seasoned fitness fanatic, the state of the art gym can help 
monitor your progress and fitness professionals will be there to coach you all 
the way, to assist you in achieving your fitness goals. With the very latest in 
diagnostic equipment, the physio team are experts in assessing injuries, 
musculosketel imbalance and limitation of movement and recommending treatments 
to help regain full range of movement.<br>
<br>
<font color="#C62C31"><b>EXECUTIVE HEALTH</b></font> Rather than just the usual 
data gathering annual check up, Al Nahda’s Executive Health Programme (EHP) is a 
meaningful and enlightening experience. It can help influence your future in a 
positive way. Prevention is always better than cure and once EHP experts know 
your state of health (both physically and mentally) they will build a programme 
that takes into account your age, medical history, lifestyle and levels of 
fitness. The team of specialists will help you find the medicine that best fits 
you! <br>
<br>
<font color="#C62C31"><b>ADVENTURES &amp; FUN</b></font> Housed in the adjoining 60 
acres, there is a small group of elephants imported from Thailand that provides 
a fantastic photo opportunity or maybe even the ride of your life! Each Elephant 
complete with mahout has been trained at Ayutthaya Elephant Park, a short trip 
from Bangkok. Arabia is famous for its camels, known as the ships of the desert 
they are a very important element of Omani life even today. Camel racing is 
taken very seriously and a fast camel is worth tens of thousands of rials. The 
place has a small caravan of camels so you too can travel by desert ship. One 
can take a tour of the nearby Orchid Farm and Green House. Teeb is a very famous 
brand of perfume both in Oman and overseas known for high quality toiletries and 
perfumes. Al Nahda is situated just a short distance away from their perfumery, 
a chance to view a working still! <br>
<br>
The Dimaniyat Islands are the jewels in the crown for diving in the Gulf of 
Oman. The resort has teamed up with Oman’s premier dive company Global Scuba 
Centre for diving the Dimaniyat. These tiny islands lie about 18 km off the 
Batinah Coast and are Oman’s only marine national reserve protected by UNESCO 
for over 20 years. Diving here is spectacular. Coral reefs cover up to 70 per 
cent of the dive sites seabed with more than a hundred different species of soft 
and hard coral. With marine life varying from sting-rays, occasional turtles, 
leopard sharks and on rare occasions you can come across the whale shark. </font></p>
