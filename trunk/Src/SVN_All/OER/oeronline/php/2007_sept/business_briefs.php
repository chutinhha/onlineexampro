<img border="0" src="bus7.jpg" width="200" height="122" align="left" hspace="10">Inflation hits 4.7% on 
rising prices<br>
</b></font><font face="Verdana" size="1">Rising prices of food and non-food 
items pushed Oman’s consumer inflation to 4.7 per cent in June this year, 
official sources have revealed. The rise came mainly due to the increase in the 
prices of food, non-food, manufactured items, rent and water. The consumer price 
index (CPI), which is the widely followed measure of inflation, rose by 4.7 per 
cent in June 2007 as against 2.9 per cent in June 2006, an increase of over 62 
per cent or 1.8 percentage points.<br>
