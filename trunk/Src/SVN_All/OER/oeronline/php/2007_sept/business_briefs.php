<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font color="#CC3300" size="2" face="Verdana"><b>Yenkit mulling US$2 
billion tourism project near Muscat<br>
</b></font><font face="Verdana" size="1">Yenkit Tourism Development LLC (YTD) is 
seeking to develop a US$2-billion luxury integrated tourist resort complex, 
20-km southeast of Muscat on Oman’s eastern coastline. YTD has been set up by a 
group of private Omani investors and the Majid Al Futtaim Group to plan, develop 
and manage this signature 5-star complex. The 950-hectare Yenkit community 
project will be an ‘integrated tourism complex’ project as designated by the 
Oman’s Ministry of Tourism, and will consist of four 5-star hotels with 
approximately 900 rooms, an 18-hole international golf course; a wide range of 
facilities, including sports and leisure infrastructure, a resort village, 
nature reserve and visitors’ centre, beach club, heritage and craft centre; and 
major open spaces; 1,400 residential villas and up to 1,900 residential 
apartments and townhouses. The site is being planned as a high-end 
environmentally sensitive project, specifically designed to reflect the natural 
form of the land. The master plan has been approved and development work is 
expected to start in early 2008, subject to finalisation of agreements with the 
Omani Government.<br>
&nbsp;</font></p>
