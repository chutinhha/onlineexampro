<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>NBO signs largest 
syndicated loan</b></font><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="bus2.jpg" width="250" height="167" align="right" hspace="12"></b></font><font face="Verdana" size="1">National Bank of Oman has signed a US$325 million syndicated term loan facility 
with a consortium of international banks. The facility was heavily 
oversubscribed with 18 banks joining in the syndication. It has a maturity of 
five years and the proceeds will be used for general financing purposes. 
Sayyidah Rawan Al Said, NBO’s Deputy Chairperson said: “The loan was syndicated 
at a particularly uncertain time for the loan markets globally, but despite the 
turbulence currently being seen in the US sub-prime market, we achieved a 
healthy oversubscription and the Facility was scaled-back. The success of the 
loan also reinforces primarily the strength, international standing and repute 
of NBO. It also reflects the importance of our banking relationships throughout 
the world, with banks from eleven different countries committing to the 
transaction.”<br>
&nbsp;</font></p>
