<img border="0" src="bus5.jpg" width="250" height="150" align="left" hspace="10" vspace="4">Increase in traffic for 
Oman Air<br>
</b></font><font face="Verdana" size="1">Oman Air in the first six months of 
2007 carried 643,724 passengers. The national carrier of Oman carried 601,161 
passengers in 2006 and 506,545 passengers in 2005 during the same period, 
respectively. Ziad bin Karim Al Haremi, chief executive officer of Oman Air, 
said: “Oman Air continued to break its former records and we are pleased with 
such outstanding achievements. We are enthusiastic with the increase in 
passengers’ traffic, but at the same time, and based on the company’s strategy, 
which aims to provide the best of services, we are dedicated to provide the 
highest degree of safety and reliability on regular basis, in spite of the 
increase in fuel prices and sturdy competition among airlines”. He added that 
Oman Air operated 6,727 flights during the first-half of 2007, thus raising the 
seat factor to 78 per cent.<br>
