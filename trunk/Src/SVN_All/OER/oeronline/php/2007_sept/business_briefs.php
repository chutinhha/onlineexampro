<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>Khareef draws record 
crowd<br>
<img border="0" src="bus4.jpg" width="250" height="166" align="right" hspace="10" vspace="2"></b></font><font face="Verdana" size="1">The Khareef Festival this year has been 
a major hit with record number of tourists visiting at Salalah. Nearly 95 per 
cent of the record number of holidaymakers visiting the Khareef Festival are 
from Oman and the other GCC countries, as per the statistics compiled by the 
National Economy Ministry in cooperation with Ministry of Tourism and Royal Oman 
Police. The count of tourists between June 21, when the Khareef season began, 
and August 17 was an all-time high of 306,007, compared with 241,861 in the 
corresponding period of 2006. While the government is making all out efforts to 
woo more tourists to Salalah, known as the land of frankincense, private sector 
companies have set up shop in a major way at the Khareef Festival grounds which 
has become the beehive of all activities. Major corporate entities like Oman 
Air, Omantel, Nawras, BankDhofar, HSBC Bank, BankMuscat and other companies 
dealing with consumer goods have all upped their ante to cash in on the huge 
influx of visitors to Salalah.<br>
