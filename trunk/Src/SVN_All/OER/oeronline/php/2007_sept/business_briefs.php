</font><font face="Verdana" size="2" color="#CC3300"><b>Galfar’s IPO making 
waves</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bus1.jpg" width="250" height="156" align="left" hspace="10" vspace="4">Galfar Engineering &amp; Contracting SAOG (under transformation) has entered the 
capital market through an IPO of equity shares of face value of Baisas 100 each, 
with a premium of Baisas 500 and issue expenses of Baisas 2 for cash aggregating 
RO 60.2 millions. The IPO is a combination of an issue of 40 million new 
ordinary shares by the company as an increase in capital and an offer for sale 
of 60 million existing ordinary shares by the promoters/current shareholders. 
Both Omani and non-Omani individuals, corporate bodies, institutions, investment 
funds, pension funds can apply for the shares. The issue opened on August 12 and 
is expected to close on September 10. The company has an authorised share 
capital of RO 50 million. Post IPO, the promoters will hold 60 per cent of the 
paid-up share capital and the public rest of the 40 per cent. The issue proceeds 
from the new ordinary shares accruing to the company will be used for capital 
expenditure and long-term working capital needs.<br>
