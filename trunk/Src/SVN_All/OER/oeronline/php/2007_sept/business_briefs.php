<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="bus3.jpg" width="213" height="142" align="left" hspace="10">Omantel’s net up 12.9%</b></font><font face="Verdana" size="1"><br>
Oman Telecommunications Company SAOG (Omantel) has posted a profit after tax 
(excluding minority interest) of RO48.475 million during the first half of 2007 
compared to RO42.963 million for the corresponding period in 2006, a growth of 
12.9 per cent. The company said: “The total group revenue as of June rose by 
11.6 per cent to RO175.918 million compared to RO157.579 million in the 
corresponding period of the previous year. Operating expenses increased by 13.4 
per cent to RO121.360 million compared to RO107.016 million in the year-ago 
period. The major increase in operating expenditure came from interconnection 
expenses, depreciation, commission and royalty charges. The earning per share 
(EPS) as of June 2007 was RO 0.065, 12.9 per cent higher than year-ago levels. 
Total subscriber base (all services) increased to 1.73 million as of June 2007 
compared to 1.54 million of the corresponding period of previous year, a growth 
of 12.4 per cent.<br>
