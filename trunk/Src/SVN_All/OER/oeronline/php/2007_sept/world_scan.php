</font><font face="Verdana" size="2" color="#CC3300"><b>Have iPhone in Oman</b></font><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="world2.jpg" width="200" height="134" align="right" hspace="10"></b></font><font face="Verdana" size="1">Within few weeks of the launch, Apple’s iPhone on AT&amp;T network has been hacked. 
The first and the most celebrated iPhone hacker is an American teenager George 
Hotz. With a little soldering, a lot of command-line computing and about two 
hours of time, Hotz was able to switch an iPhone over to T-Mobile, which he 
documented in detail in a YouTube video and on his blog. The iPhone runs on GSM 
networks, which only T-Mobile and AT&amp;T Wireless run in the US Hotz collaborated 
online with four other people, two of them in Russia, to develop the unlocking 
process.<br>
<br>
Apple may be able to counter the hacks, either with a software patch 
downloadable through iTunes or with physical changes on the iPhone production 
line. Although even hacked iPhones have to be bought from Apple, the company is 
reportedly getting a cut of the monthly subscription fees from AT&amp;T. <br>
<br>
The real market for unlocked iPhones is in Europe, where the iPhone is not 
scheduled to be on sale until sometime this fall, and in the rest of the world, 
which with the exception of South Africa won’t see it until next year. <br>
