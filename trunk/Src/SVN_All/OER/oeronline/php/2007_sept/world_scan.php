<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>Monster.com admits theft 
of 1.3 million job seekers’ info</b></font><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="world.jpg" align="right" hspace="10" vspace="4"></b></font><font face="Verdana" size="1">Monster.com waited five days to tell its users about a security breach that 
resulted in the theft of confidential information from some 1.3 million job 
seekers. The hackers broke into the US online recruitment site’s 
password-protected resume library in one of the biggest Internet security 
breaches in recent memory.<br>
<br>
According to Monster, the information stolen was limited to names, addresses, 
phone numbers and e-mail addresses, and no other details, including bank account 
numbers, were uploaded. Pretending to be sent through Monster.com from job 
recruiters, the e-mails asked recipients to provide personal financial data, 
including bank account numbers. They also asked users to click on links that 
could infect their PCs with malicious software. Monster’s database has about 73 
million resumes.<br>
