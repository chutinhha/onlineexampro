<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="world1.jpg" align="left" hspace="10">WHO says more killer 
diseases like AIDS on their way</b></font><font face="Verdana" size="1"><br>
The World Health Organisation (WHO) has warned the world to expect a new killer 
disease to emerge in the next few years which will threaten the lives of 
millions.<br>
<br>
The WHO says the new disease will have the impact of HIV/AIDS or Ebola and 
potentially deadly new diseases are being identified at an “unprecedented rate” 
with at least one new disease identified every year since the 1970s.<br>
<br>
There are now 39 more diseases that were unknown just over a generation ago and 
the WHO says global epidemics are spreading more rapidly than ever. The UN 
agency says infectious diseases are spreading faster due to global travel and a 
worldwide market in live animals. There are now more than 2 billion global air 
travellers each year, says the WHO and this means an outbreak or epidemic in one 
part of the world is only a few hours away from becoming an imminent threat 
somewhere else. The agency maintains the next influenza pandemic is likely to be 
a bird flu one which could affect around 1.5 billion people and the question is 
one of when rather than whether.<br>
