<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3300"><b>
<img border="0" src="gcc14.jpg" width="175" height="263" align="left" hspace="10">Walmart wants to bite 
into $50-billion Indian retail market</b></font><font face="Verdana" size="1"><br>
It is the turn of many Indian retailers to lose their bread-and-butter as 
Walmart enters Indian market. While the Indian government’s decision to 
partially open the retail sector to foreign players has been criticised by small 
traders, Walmart has tied up with India’s Bharti Enterprises to set up a retail 
base in the country. <br>
<br>
As part of the agreement, the US-based retailer would handle back-end operations 
of the joint venture company. With Bharti as its partner, Walmart has some 
ambitious plans to take over India’s retail market, which is worth a whopping 
US$50 billion. To make the most of the wholesale opportunity, Walmart will 
invest several millions of dollars into setting up supply chain infrastructure 
in India. The company is in talks with local logistics’ providers and suppliers 
in India to source for its B-to-B wholesale cash and carry joint venture with 
Bharti.<br>
<br>
“We are looking at local warehouses, trucking companies and refrigeration 
manufacturers. We will certainly have our own warehouses and distribution 
centers across India,” said Raj Jain, Country President, Walmart India.<br>
<br>
The Bharti-Walmart alliance is also talking to global suppliers in China, US and 
Latin America to join them in India. Walmart is currently sourcing products 
worth US$600 million from India and the figures are expected to multiply once 
the cash-and-carry stores start rolling out by the end of 2008.<br>
