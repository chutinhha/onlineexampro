<br>
Jawad Sultan Technologies LLC (JST) offers the widest selection of ultra 
reliable business and industrial printers, home printers and printing solutions. 
With this wide range of products, JST offers business-to-business printing 
solutions dedicated to creating professional printed documents. The printing 
solutions offered will meet customer requirements of not just the present but 
also the future.<br>
<br>
The product line includes networked laser colour and monochrome printers of A4, 
A3, low and high volume MFPs, low and high volume digital copiers, low and high 
volume dot-matrix, line printers, Inkjet printers etc. With multifaceted 
products, solutions and services, JST has built a reputation of being a reliable 
partner specialising in business class printing solutions. JST is a major player 
in the printer market and is dedicated to offering relevant, high performance 
products, applications and services to meet the printing requirements. The 
printing solutions offered will help increase the efficiency and control the 
printing costs.<br>
<br>
The advanced technologies and customised solutions offered by JST will keep your 
business running smoothly and efficiently. The high volume products provide the 
best printing solutions for all the major industries such as retail, healthcare, 
automotive, manufacturing, telecommunications, government, aviation, 
transportation and warehousing/distribution. Solutions are also available for 
customers with lower printing volumes. Reliability, professionalism and 
competence are some of the values that JST brings to its customers. This is 
evident in the growing market share of JST. The demand for high quality 
products, solutions and services is growing and JST’s three decades of 
experience combined with quality staff and infrastructure makes it possible for 
JST to meet this challenge.<br>
<br>
JST’s partners:<br>
<br>
<b>UTAX<br>
</b>It’s a Germany-based company, established in 1961 with headquarters in Hamburg. 
UTAX is a prominent name and is in the business of copiers, printers, MFPs, and 
fax. It is a leading brand in both Germany and outside Germany.<br>
<br>
<b>OKI printing solutions</b><br>
OKI printing solutions is a global business-to-business brand dedicated to 
creating professional printed communications products, applications and services 
which increase the efficiency of today’s businesses. From printers to faxes and 
multi-functional products through applications and services, OKI printing 
solutions has brought to market a range of incredibly fast and high performance 
machines that regularly win awards and beat the competition. OKI is a Japanese 
company with a multinational presence.<br>
<br>
<b>Lexmark</b><br>
Lexmark International, Inc is a leading developer, manufacturer and supplier of 
printing solutions - including laser colour, mono printers, multifunction 
products, and inkjet printers, dot-matrix printers, associated supplies and 
services.<br>
<br>
Lexmark was the first company to market 4800 x 1200 dots per inch (dpi) and 3600 
x 1200 dpi resolutions, develop 1200 dpi laser and inkjet printers, and produce 
standalone photo printers.<br>
<br>
<b>TallyGenicom</b><br>
Tally and Genicom have teamed up to form the new entity called TallyGenicom. 
With over 50 years of experience and with operations across the globe, 
TallyGenicom is now one of the largest companies in the world specialising in 
business-class printers. Prior to the merger of Tally and Genicom on August 7, 
2003, Genicom LP had been a leading provider of printing solutions, focusing on 
the mid-range transaction document printing market. Tally AG was founded in 1948 
and had been a worldwide leader in the design, development, manufacturing, 
distribution and servicing of printers for high-volume industrial and business 
applications. Tally was a key innovator in line matrix and industrial ink jet 
technologies and had become an important player in the colour laser market. 
Headquartered in Ulm, Germany, Tally’s American operations had been based in 
