      <p align="justify"><font face="Verdana"><b><font size="1">‘Mobile provides unparalleled reach’<br>
</font></b><font size="1"><b>Aneth Arosemena, the ex division head – marketing &amp; 
sales of Oman Mobile talks about the service provider’s strategy on mobile 
advertising and the related issues</b><br>
<br>
<b><img border="0" src="market3.jpg" width="233" height="248" align="left" hspace="10" vspace="4">What is your opinion about mobile advertising?</b><br>
Mobile services around the world have become a commodity. Big companies are 
taking advantage of that by advertising on mobiles.<br>
<br>
<b>Don’t you think that it is an invasion of customers’ privacy?</b><br>
In Oman, the customers have a choice. They can subscribe to the service they 
want. People are becoming more and more exposed to the media. Oman Mobile’s 
customers have the choice of visiting our outlets or website and tell us whether 
they wish to receive the commercial sms’s or not. There is a customization 
option also where the subscribers can subscribe to receive sms from specific 
companies. There is a simple process of opt in or opt out but on average, only 
2-3 percent of our customers have opted out.<br>
<br>
It is more than a revenue generator for Oman Mobile as it is also a customer 
retention tool for us. The customers love these kinds of things. Don’t forget 
that 65 percent of Oman’s population is below 30 years of age. Lots of 
youngsters like this kind of exciting new choices.<br>
<br>
<b>How has been Oman Mobile’s experience on mobile advertising?<br>
</b>We have been providing this service for the last three years. In the first 
year, we promoted the Gamer Challenge for university and college students, and 
we have continued to invest in that on an annual basis. It has been a great 
success. In 2005, we also did prominent mobile campaigns such as Toyota and 
Pizza Hut. In 2006, we did 22 such campaigns and so far this year we have done 
14.<br>
<br>
As time goes by, more and more people will see the value of reaching out 
everywhere in Oman. Who knows how far the traditional print media reaches. If I 
go to Salalah, the newspapers are available in very select places. With such 
limited reach of traditional media, reaching out to the end user is quite a 
challenge for the marketer. Oman Mobile currently reaches more than 1.5 million 
people, more than half the population in Oman.</font></font></td>
