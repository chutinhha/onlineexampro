<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font size="4" face="Verdana">The THIRD SCREEN<br>
</font><font size="1">&nbsp;</font></b><br>
<font face="Verdana">
<font size="1"><b>After TV and PC, the mobile is globally considered as the most 
powerful screen. With the integration of television, computing and Internet 
applications in mobile devices and services, the mobile </b></font><b>
<font size="1">screen is fast emerging as an effective personalised platform for 
advertising. Akshay Bhatnagar looks at the global trend in mobile advertising 
and takes the opinion of the stakeholders in the Omani market </font></b>
<font size="1"><br>
<br>
<img border="0" src="market.jpg" width="150" height="278" align="left" hspace="10" vspace="4">A few weeks ago, Sir Martin Sorrel, the CEO of WPP Plc, while 
talking about the mobile said: “The driver to the Internet will be mobile. 
What’s driving the two (India and China) is not the PC, but the mobile. Given 
the economics of it all, mobile will be cheap and people who say that the screen 
is not big enough to watch TV, say it because they were used to watching TV like 
I have. If you’re 50 or 60 (years old), your habits are formed by what you did 
when you were five or 10 years old…they are looking at things in a totally 
different way. So, things are changing.”<br>
<br>
He further said, “You want to get away from the commodity end of the market and 
try to develop things that have value to consumers, be it knowledge or 
information or services or products. I think mobile advertising will be 
significant. It’s just a question of coming up with the right creative 
approaches, the right content. We have interesting assets in this space, 
including a mobile advertising network operating in Europe and the US. And it’s 
an opportunity.”<br>
<br>
When the CEO of the second biggest advertising company in the world is so 
bullish about mobile advertising, we have to take the mobile ad platform 
seriously. The numbers are already significant. As per market estimates, US$255 
million were garnered from mobile advertising in 2005 in Europe and the US. 
Advertising on mobiles is expected to generate global revenues of US$1.9 billion 
this year, according to ABI Research. They are likely to skyrocket by the end of 
the decade. In another forecast, the industry watcher Informa Telecoms and Media 
has predicted that by 2011, mobile advertising will be worth US$11.5 billion.<br>
<br>
<b>Strength of the medium<br>
</b>The sheer reach of the mobile phone is phenomenal. Globally there are more 
mobiles than PCs. Mobile phone services reach out to more than two billion 
people by way of voice, text and increasingly multimedia tools. The numbers are 
increasing rapidly. Around one billion mobile phones are expected to enter the 
market globally in 2007.<br>
<br>
The mobile phone provides a unique opportunity to interact directly with the 
consumer. Unlike a newspaper or even a PC, a single person uses the mobile. “The 
mobile phone is a very personal device that most people carry with them 24 hours 
a day. It affords advertisers an opportunity to present very targeted and 
time-sensitive information that is of interest to the user. That is a key 
advantage. With customer permission, advertisers can collect valuable 
demographic and behavioural information to hone the marketing message,” said 
Marcia Kaplan, an industry analyst.<br>
<br>
Experts say that in a couple of years, the brands will know what works on this 
platform and then mobile advertising will become mainstream. They expect the 
market to move from conventional sms-based mobile advertising to more 
sophisticated multimedia advertising.<br>
<br>
With mobile content already incorporating advertising-like live TV programming, 
brands and entertainment content providers are beginning to see the value of 
presenting full multimedia ads with programmes. Players across the value chain 
are developing their strategies and positioning themselves in this space. 
Operators in the US and Western Europe are currently either testing various 
forms of advertising with 3G services or are allowing ads to be served on their 
portals. Furthermore, the entry of large online search engines into the mobile 
world opens up new advertising opportunities in the shape of context-based 
mobile search.<br>
<br>
Mobile operators, who have been reluctant to leverage their customer base for 
mobile marketing purposes, now see an opportunity to create a new revenue 
stream. In Europe, HotSMS in the Netherlands delivers a free, ad-sponsored SMS 
offer. Blyk in the UK and i-Wood in the Netherlands are reportedly preparing to 
launch free or heavily subsidised voice and data services in exchange for mobile 
phone users’ consent to receive advertisements.<br>
<br>
Vodafone Group, Europe’s largest mobile phone operator, is working separately 
with search engine giants Google and Yahoo. Google is developing a service that 
would link advertising to Internet searches – a counterpart to its flagship 
business of selling ads linked to regular Internet searches. With Yahoo, 
Vodafone aims to jointly develop advertisements in a variety of formats, 
including banner ads and short videos. To help make ads interesting to 
customers, the operator plans to offer discounts on services such as mobile TV, 
games and multimedia messaging service. In the US, Sugar Mama owned by Virgin 
Mobile, gives prepaid customers additional minutes by watching online ads, 
answering questions by sms or filling out surveys about products and services. 
AT&amp;T, Sprint Nextel, and Verizon Wireless have also signalled interest in 
developing mobile advertising strategies of their own.<br>
<br>
<b>Move tactfully<br>
</b>No doubt the road ahead is promising but there are a lot of issues that need 
to be addressed by the industry stakeholders. Despite the lure of the revenue, 
mobile operators are too busy ramping up the numbers to look for value-added 
services that drive mobile advertising. As a result, the full potential of the 
medium has not been realised. <br>
<br>
Another critical issue is, how to make sure that consumers don’t get annoyed 
with ads arriving on a very personal device. Not every mobile user gets amused 
with mobile ads. Different markets behave in a different way. As per a survey, 
only 18 per cent of respondents in the US said they were receptive to the idea 
of watching ads in exchange for free mobile content. The percentage was higher 
in Europe, where 37 per cent said they would do so.<br>
<br>
We must also understand that standalone ads on mobile phones will not deliver 
results if they don’t get integrated into an overall marketing strategy which 
also takes into account promotion, sales and customer services, as highlighted 
by a Datacomm Research report. According to the research, inundating mobile 
subscribers with sales pitches won’t be effective. Successful merchants will be 
those who leverage mobile phone technology to create deeper and longer-lasting 
customer relationships. Issues to be resolved include business models and 
revenue share; the type, length and frequency of ads, consumer attitudes and 
many others. Operators will have to walk a fine line between maximising the 
revenue potential of advertising and not alienating the subscribers.<br>
<br>
<b>OMAN –
A long way to go</b><br>
<img border="0" src="market4.jpg" align="left" hspace="10" vspace="4">The two million plus mobile subscribers in Oman (TRA figures as on May 2007) are 
divided between two operators – Oman Mobile and Nawras. The market in Oman is 
still in an early stage as far as mobile advertising is concerned. As of now, 
Oman Mobile allows commercial SMS on its network whereas Nawras doesn’t allow 
it. However, they both allow SMS that are in the social interest.<br>
<br>
Service providers, ad agencies and marketers are all interested in the medium 
but are cautious as they are not yet convinced about its viability. But some 
stakeholders have used the medium strategically to a great advantage. OER spoke 
to some of the key industry players to get a better understanding of their views 
and experiences of the medium.<br>
<br>
Saibal Basu, General Manager-Oman of Landmark Group that operates leading retail 
brands such as Centrepoint, Home Centre, Emax, Lifestyle, etc., has successfully 
used the medium to a great extent. Sharing his strategy, he said, “We send 
tactical SMS. We have a customer loyalty programme called Centrepoint privilege 
card which has a membership of about 50,000. We send SMS to them. As we know 
them, we tailor-make the message, say in English or Arabic. We do phase-wise 
communication. A day or two after the SMS to our exclusive customers, we mass 
broadcast it through Oman Mobile. The SMS is integrated into our marketing 
communication plan that also includes press ads or outdoor promotions, etc.” 
Does it translate into business? “With just a special sale offer SMS to our 
privileged customers, the footfalls increase by three-to-four times in 
Centrepoint; it goes up by 10 times in Home Centre,” he replied.<br>
<br>
The banking sector has embraced the new media including mobile and Internet as 
part of their business process. Shaima Murtadha Al-Lawati, Manager-Corporate 
Communications, BankMuscat said, “Mobile is an essential medium for us to keep 
in touch with our customers. Mobile is an integral part of our brand 
communication campaigns and maintaining the relationship with our customers.”<br>
<br>
<img border="0" src="market1.jpg" width="200" height="251" align="right" hspace="10">She further said, “In the absence of circulation audit and presence of very few 
readership surveys in Oman it is difficult to measure the reach or opportunity 
to see (OTS) of ads in the mass media. With mobile medium, we know exactly how 
many users have got our sms. But the challenge remains in knowing how many 
actually read the message. There are some issues that needs to be tackled. The 
database segmentation must be done to deliver target specific communication. 
Secondly, we all must find ways to use the medium more creatively to ensure the 
communication message has been received by the reader.”<br>
<br>
Generally, it is the responsibility of the communication partners say ad 
agencies to give the most effective communication plan to the marketers. Do they 
actually propose mobile as part of the media plan to their clients? Eihab A. Abu 
Taha, General Manager of Sabco Art LLC, gives a fair picture: “The issue is not 
whether we do it or not. The issue is whether we know enough about it or not. 
Unlike traditional media such as print, outdoor or satellite TV, at this stage, 
we don’t know what we can and cannot do with mobile from a technology point of 
view. The traditional media marketers normally brief us about their 
communication vehicles and we are able to measure effectiveness; However, mobile 
service providers have not briefed us on using mobile from a technology point of 
view. Unfortunately as an ad agency we don’t know from a technology point of 
view what the mobile operator can do.”<br>
<br>
Atulya Sharma, Assistant General Manager, UMS Advertising opined: “The marketers 
are looking for alternative ways to reach customers. Traditional channels for 
advertising, like TV, radio and print, are becoming less effective because 
consumers now consume information and entertainment very differently than in the 
past, due largely to the digitisation of content and the increasing ease of 
access to the Internet. This is especially true of the highly coveted ‘youth’ 
demographic in Oman – the 18-to-34 year olds with high disposable income, high 
brand awareness, and short attention spans.”<br>
<br>
<img border="0" src="market2.jpg" width="200" height="261" align="left" hspace="10" vspace="4">He further stated, “We can surmise that mobile as a medium has tremendous 
potential. However, marketers and advertising agencies using the medium in Oman 
will have to bear certain codes of conduct when using the medium. For one, many 
of us will agree that a commercial SMS in the middle of a private moment can be 
very annoying. Hence, we should only engage with customers who have granted 
permission to receive commercial messages on their mobiles. Aside from the 
ethical aspect of spam, unsolicited messages do not work and cost the company 
dearly.”<br>
<br>
In order to avoid intrusion in their subscribers’ privacy, Nawras doesn’t allow 
commercial SMS. Adnan Idrees, Customer Relationship Loyalty Manager of Nawras 
said: “We don’t want to bother our customers. With commercial messages from 
third parties, it is an invasion of privacy. If at any stage we decide to 
provide for it, we will ask our customers in advance if they would like to 
receive any commercial SMS. We are about to work closely with some of the well 
renowned suppliers of mobile marketing to evaluate solutions that will add value 
to the message that an advertiser wants to send, as well as bringing excitement 
and rewards to our customers who choose to receive these messages. Nawras always 
aims to be pleasingly different, focusing totally on customers and testing all 
services extensively before introduction. If, and when Nawras uses commercial 
mobile marketing, it will be a unique and creative option for our customers.” 
The experience in other markets indicates that service providers cannot ignore 
this revenue stream for long.<br>
<br>
A keen follower of the market said, “The subscribers’ segmentation is done on 
the basis of language spoken (Arabic or English) and type of service (prepaid or 
post paid) by the service providers. The profiling is done based on the service 
that is provided because usually a post paid user is going to have a completely 
different profile than a prepaid user. In terms of age and gender it is a bit 
difficult for the service provider to provide the information. There are 
cultural issues involved. A lot of subscribers are males but many of those 
connections are actually used by the female members of their family! So the 
holders and users are different. May be as we move ahead more segmentation will 
be done with better processes for data collection, management, mining and 
cleaning.” 
