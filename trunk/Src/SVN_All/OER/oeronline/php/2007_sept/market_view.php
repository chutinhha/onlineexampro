<b>Fed’s surprise moves<br>
</b>Amidst the infamous ‘sub-prime’ worries shaking financial markets the world 
over, the US Federal Reserve surprised market participants by cutting the 
discount rate on its loans to banks to 5.75 per cent from 6.25 per cent. In a 
statement, the Fed spokesperson said downside risks to growth had increased 
‘appreciably,’ forcing it to intervene through this mechanism. The discount rate 
cut could lower the cost of capital for banks and help keep credit flowing 
through the economy at a time when investors have shown a greater reluctance to 
lend. The policy committee, popularly known as the Federal Open Market Committee 
(FOMC), held on to the view that it was monitoring conditions and was prepared 
to act as needed to mitigate the adverse effects on the economy arising from 
disruptions in the financial markets. One genuinely wonders now whether this 
means that Fed is asking the market to expect a rate cut before its scheduled 
meeting on September 18!<br>
<br>
While a rate cut will not make a difference to the current economic positioning 
nor will it transform bad debts into good ones. But what it will probably do is 
bring the ‘confidence’ back to the system that has been clearly shattered over 
the past two weeks. If the US home lending (to borrowers with low 
creditworthiness) was the epicentre of the crisis, a rate cut will do more good 
to the housing sector. Since central banks across the globe seldom move against 
the Fed’s direction, a similar cut can be expected from Bank of England and 
European Central Bank. Or at least these two major central banks will refrain 
from raising the rates further, something they have been doing for some time 
now.<br>
<br>
However, the question remains as to why should central banks around the world 
panic? Financial market corrections are quite common and probably it is not the 
first time that a raging bull market sees a sudden 10 per cent correction from 
its peaks. In recent times, be it during the May 2006 or Feb 2007 meltdown, 
there was no open market activity or rate cut by any major central bank, 
including the Fed; so why now? The only logical conclusion one can make is that 
they fear a credit-crisis to have much larger implications on the economy as a 
whole with a potential to derail the growth engine, if not send it into a 
recession!<br>
<br>
<b>BankMuscat Research Abstract<br>
</b>Based on its first half 2007 financial performance and encouraging future 
outlook, Vision Securities maintains a positive outlook on BankMuscat, the 
leading banking institution of Oman. BankMuscat continues to reap the benefits 
of being at the ‘numero-uno’ position in Oman’s fast expanding banking sector. 
This is visible from the aggressive pace at which the assets and liabilities are 
growing for the bank. Year to date, the net loan assets of the bank grew by 19 
per cent, which was supported by an 18 per cent growth in customer deposits. 
Vision views that an increased volume in its core banking activity, which leads 
to rising interest and non-interest income, has been the key drivers for the 
bank’s earnings growth during the first six months of 2007. As compared to 
June-06, BankMuscat reported a sharp 43.5 per cent growth in its earnings in 
2007.<br>
<br>
With the issue of fresh commercial banking licenses in Oman, the competition in 
the banking space is expected to intensify further. However, with its leading 
position in the local market and a large branch network, Vision expects 
BankMuscat to maintain strong growth rates in loans as well as profits during 
2007-08. And the rationale for its expectation lies in the bank’s strong grip on 
Oman’s asset/liability markets, and also its ability to generate higher fee 
income.<br>
<br>
Assuming a full year loan growth of 30 per cent and non-interest income growth 
of 25 per cent, Vision estimates an EPS of RO 0.092 in 2007 for BankMuscat. The 
book value is estimated to be at RO 0.421 by the end of 2007. The current market 
price discounts the EPS estimate by 15.4 times and the book value by 3.4 times. 
On the forward estimates for 2008, this would be 11.8 and 2.8 times, 
respectively. Given the estimated earnings growth of around 39 per cent in 2007 
and 30 per cent in 2008, Vision believes that valuations are on the lower side 
for BankMuscat stock at current levels, if one considers investing with a longer 
investment horizon. </font><hr width="90%" size="1">
