<font face="Verdana" size="1">The opening of the Galfar IPO is having some negative impact on the secondary 
market. It can be expected to accentuate towards the end of the subscription 
period (September 10). However, market participants anticipate more exciting 
days once this large-cap (Galfar) issue gets listed. <br>
<br>
<b>GCC Markets<br>
</b>The GCC markets witnessed a mix trend during the month ended August 15, with 
Saudi market being a top gainer, generating a return of 6.12 per cent for the 
period. The Saudi insurance stocks saw a huge amount of local buying and surged 
by 48.6 per cent. The bank stocks also supported the bull-run and appreciated by 
9.5 per cent during the period. The service and the cement indices grew by 3.2 
per cent and 3.1 per cent, respectively, whereas the industrial sector was 
almost flat at 0.9 per cent. The Bahraini and Kuwaiti bourses too ended in the 
positive territory generating a return of 1.9 per cent and 1.8 per cent, 
respectively. All major indices ended on the positive side on the Bahrain Stock 
Exchange, with the services sector taking the lead. However, the Kuwaiti 
exchange, which has the highest year to date return of 25.23 per cent, as 
compared to other markets, saw lacklustre performance in the industrial, 
insurance and banking counters, which declined by 2 per cent, 0.5 per cent and 
0.1 per cent, respectively. The Abu Dhabi and Dubai markets witnessed selling 
pressure from foreigners towards the end of the period and dipped 5.5 per cent 
and 5.3 per cent, respectively. The Abu Dhabi banking sector and the Dubai 
investment sector were the worst hit and declined by 5.1 per cent. The Doha 
Securities Market too ended on a bearish note and reduced by 4.1 per cent for 
the month ending August 15. The insurance, banking and services sector witnessed 
unexciting performance and dropped by 5.8 per cent, 5.1 per cent and 4.4 per 
cent, respectively.</font><p align="justify" style="margin: 3px 10px">
