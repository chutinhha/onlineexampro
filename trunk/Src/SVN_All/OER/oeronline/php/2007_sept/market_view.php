</font><b><font face="Verdana" size="4">Bucking the trend</font></b><font face="Verdana" size="1"><br>
<br>
<b>World financial markets are in turmoil as worries about tightening credit 
conditions spread to the money market area, but Muscat led the bounce back in 
some GCC markets</b><br>
<br>
It was the second-best performance for a month by the Muscat Securities Market (MSM) 
after May-June 2007. The benchmark MSM-30 index gained 333 points or 5.2 per 
cent during the period, propped up mainly by the banking and industrial indices. 
Market activity as measured by the total turnover also increased marginally by 
almost 3.8 per cent to RO111.5 million, mainly due to lower institutional 
participation. Many key stock counters, specially of investment companies like 
OMINVEST, DIDIC, etc., witnessed increased volumes on positive news from the 
first half numbers. Banking shares literally ruled the roost by clocking in the 
biggest gains, specially Bank Dhofar, BankMuscat and NBO. More than domestic 
demand, foreign investors appear to be chasing the fast-growing banking shares 
in Oman.<br>
<br>
In key corporate events, a consortium consisting of Port Services Corporation (PSC) 
and IQ Martrade, Germany, which specialises in shipping and port management, 
have got the initial approval from the Government of Oman to manage and operate 
Port of Khasab in Musandam Governorate on concession agreement basis. The PSC 
said in a communiqué that in case of final agreement, the company would 
establish a joint venture with the German company for managing and operating the 
port.<br>
<br>
In another significant development, BankMuscat announced that its board has 
approved a proposal to raise capital through private placement of 15 per cent of 
its shares with Dubai Financial Group, a ‘Dubai Holding’ group company, for 
RO238.3 million (US$619 million). The price per share works out to be RO1.475 
for this deal. The bank claimed that this was the single largest cross border 
investment in the banking sector in the region.<br>
<br>
Omantel’s management said that its plans to go ahead with the purchase of 
Pakistani World Call Telecom Company were still under procedures and no final 
decision had been taken. However, the company confirmed to the market regulator 
that it would purchase 65 per cent of World Call before end of August.</font><p align="center" style="margin: 3px 10px">
