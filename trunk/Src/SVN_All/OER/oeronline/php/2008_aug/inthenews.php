<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Government plans to sell 
25pc stake in Omantel<br>
</b></font><font size="1" face="Verdana">
<img border="0" src="news1.jpg" align="right" hspace="6" vspace="4" width="200" height="133">The 
government has made plans to sell 25 per cent stake in Omantel (Oman 
Telecommunications Company) by the end of this year and has sought expression of 
interest from qualified strategic investors. Currently 70 per cent of Omantel is 
owned by the government and the rest is traded on the MSM. Once the 25 per cent 
sale takes place, government ownership will fall to 45 per cent. The investor 
should have operations in multiple countries and at least 5 million active 
subscribers of which a minimum of 2 million in one market. <br>
<br>
Apart from 
investment, the strategic investor is expected to contribute with management, 
technology and strategic planning at Omantel. The purpose of the sale is to make 
Omantel a worldclass service provider both inside and outside the Sultanate. As 
part of the sale process, the bidders will first submit a technical proposal on 
their experience and credentials and a financial proposal on what they would be 
willing to pay, said a company spokesperson. In conjunction with the sale, the 
government may also consider providing the strategic investor with certain 
rights enabling it to increase its economic and voting exposure to Omantel in 
future. <br>
<br>
Citigroup Global Markets Limited is the lead adviser and National Bank 
of Oman is the local adviser for the sale. Omantel is the sole fixed line 
service provider in Oman with 270,000 subscribers in 2007 and the market leader 
in mobile communications with 1.49m subscribers, representing 60 per cent market 
share in 2007. <br>
<br>
It acquired a 65 per cent stake in Pakistan’s Worldcall in 
February 2008.<br>
