<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Exploring possibilities</b></font><font size="1" face="Verdana"><br>
<img border="0" src="news2.jpg" align="left" hspace="6" vspace="4" width="200" height="160">The 
government acting through the ministry of finance has announced its plans of 
exploring the partial sale of its shareholding in Oman Telecommunications Co (Omantel). 
H E Darwish Ismail Al Balushi, Secretary General, Ministry of Finance speaks 
exclusively to OER about the process. <br>
<br>
<b>How was the 25 per cent figure for a stake sale arrived at? </b><br>
<b>HE Balushi:</b> We 
thought that 25 per cent is adequate and proper to encourage the investor as we 
felt that anything less than that would not be attractive to investors. The 
government feels that the disinvestment has to be done gradually. So as a first 
step, a 30 per cent stake was sold and now we are divesting 25 per cent stake 
bringing down the government’s share to 45 per cent. It is likely that an 
investor may look for a 25 per cent plus stake, but they will also look at the 
market in which they are going into and the state of a company and the services 
that it offers. We will see the value that the foreign stake holder brings in 
and if there is a need we are open to the idea of looking at the other 
requirements of a stakeholder. <br>
<br>
<b>Omantel is undergoing a merger process with Oman Mobile; is this a good time to 
bring in a new partner? </b><br>
<b>HE Balushi</b>: This is one of the reasons why we are 
starting the stake sale process now. This is part of a package of procedures 
approved by the government. The decision was driven by the internal preparations 
like the integration and the composition of the new board of directors that was 
going on. Omantel on its part is in an advanced stage of completing this 
process. <br>
<br>
<b>Will there be a clash of cultures between the new private player and Omantel 
which is a government company? </b><br>
<b>HE Balushi:</b> Omantel has transformed itself from 
being a company with a public sector ethos (mentality of a government run 
company) to a culture that is closer to the private sector. This culture has 
contributed to bringing in new standards of corporate governance and a better HR 
culture. Omantel’s efficiency is reflected in its profitability and share price. 
I see no clash in cultures between Omantel and the new player. On the contrary 
there are going to be complementary strengths that both can draw upon. <br>
<br>
<b>Omantel has taken a stake in Pakistan’s Worldcall. Will the telecom company look 
at more such opportunities in future? </b><br>
<b>HE Balushi:</b> I don’t see any reason why it 
should not; every other telecom company is looking at gaining a global 
footprint. It is an industry in which people come and compete with you on home 
ground so a local company can go abroad and compete with them. Omantel has the 
strength and experience to go international. The strategic partner will help and 
support it in exploring these opportunities.</font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1">
