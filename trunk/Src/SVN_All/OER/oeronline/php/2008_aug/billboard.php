        <img border="0" src="bill2.jpg" align="left" hspace="6" vspace="4" width="175" height="117">A’Saffa 
        Poultry’s Khareef celebrations</font></b><font size="1" face="Verdana"><br>
        A’Saffa Poultry Farms has put up a stall at Salalah Tourism Festival and 
        has a number of promotional activities lined up. As part of the 
        activities, A’Saffa is advertising in hypermarkets through booklets and 
        pamphlets and cooking chicken the traditional Omani way called ‘Madhabi’, 
        which involves taking fresh chicken and cooking it in hot stones and 
        adding the traditional spices to it, to bring out its inherent flavour.<br>
        <br>
        To involve the family, A’Saffa is also conducting several games, radio 
        shows and puzzles for children; surprise gifts are in the offing as 
        well. Says Mohammed Ahmed Shanfari, Assistant CEO (Admin Affairs), 
        A’Saffa Poultry Farm, “last year we had 26,000 visitors at our stall and 
        this year we are expecting around 40,000 visitors. Tourists coming from 
        all over the Gulf, Europe and Asia like our products, especially the 
        Madhabi. The way it is cooked and the freshness of our chicken, only 
        makes people want to eat more.”<br>
