        </font></font></b>
        <font size="1" face="Verdana">The Wave, Muscat recently extended its 
        support towards the Oman Association for Disabled by enabling them to 
        buy adaptive equipment to customise 10 cars for disabled drivers. In 
        this regard, Nick Smith, CEO of The Wave, presented a cheque to Yahya Al 
        Amri, Chairman, Oman Disabled Association.<br>
        <br>
        “The integration of handicapped people into society is an essential part 
        of life today and we at The Wave, Muscat are delighted to be able to 
        help, even in this small way, to improve the lives of those less 
        fortunate than ourselves,” said Nick Smith commenting on the 
        Association’s endeavours for the disabled.<br>
