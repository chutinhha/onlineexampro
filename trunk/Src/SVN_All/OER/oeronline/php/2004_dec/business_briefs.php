<p align="justify"><font face="Verdana" size="1"><b>PDO output falls to
660,000bpd</b> <br>
The 60 per cent government-owned
Petroleum Development Oman (PDO)
has said its daily production has
declined to 660,000 barrels, and may
fall further to 635,000 barrels a day in
2005. The daily average production of
for the year ending December 31, 2004,
is expected to be 656,000 barrels. “The
fall in our oil production is indeed a
cause for concern. Nevertheless, the
year 2004 will be judged to have been a
good one for PDO as we are in a position
to finish the year slightly above
our average annual production target
of 650,000 barrels a day. More significantly,
we will bottom out in terms of
production next year,” PDO managing
director John Malcolm said. PDO’s
production accounts for more than 90
per cent of Oman’s oil production.</font></p>

