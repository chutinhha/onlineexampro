<p align="justify"><font face="Verdana" size="1"><b>CBO to issue RO52m
development bonds
<br>
</b>The Central Bank of Oman (CBO) began
offering the 34th issue of government
development bonds. The value of the
new issue is RO52 million with a maturity
period of five years and 321 days, and
will carry a coupon rate of 5 per cent.
The issue will be open for subscription
from November 20, 2004, to December 9,
2004, while the auction will be held on
December 21, 2004. The issue date will be
December 15, 2004. Interest on the new
bonds will be paid semi-annually (i.e.,
May 1 and November 1, every year) until
maturity. Investors can apply for these
bonds through either the competitive
or non-competitive bidding process.
Investors may submit bids through
licensed banks operating in the
Sultanate anytime during the subscription
period.</font></p>

