<p align="justify"><font face="Verdana" size="1"><b>Increasing business
confidence in AGCC</b> <br>
In its latest survey, the HSBC-MEED
Middle East Business Confidence Index
has recorded a continuing rise in business
sentiment among a large group of
business leaders and observers around
the Middle East. The survey was carried
out in the last two weeks of October by
YouGov, a leading UK-based market
research agency.<br>
&nbsp;Respondents include commercial
managers, procurement executives,
recruitment executives and financial
officers as well as CEOs and chairmen of
domestic and international companies.
Published in the Middle East Economic
Digest (MEED), the survey shows that
the index, set at 100 when it was
launched in June this year, has now
reached 104.91. The index is based on an
online questionnaire, which seeks views
on a wide range of issues facing businesses
in the Middle East. The principal
countries covered by the survey are: the
UAE, Bahrain, Qatar, Oman, Jordan,
Saudi Arabia, Egypt and Kuwait.

