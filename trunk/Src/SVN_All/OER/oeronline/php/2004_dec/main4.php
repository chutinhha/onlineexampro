<p align="justify"><font face="Verdana" size="2" color="#FF0000"><b>The art of 
professional presentation</b></font><font face="Verdana" size="1"><br>
<b>
<img border="0" src="im33.jpg" align="right" hspace="5" width="100" height="118">Alan 
Stevens:</b> The single most important aspect of professional presenting is to 
understand your audience. Far too many speakers prepare and deliver their words 
of wisdom without giving a thought to what the audience want to hear,” according 
to Stevens. <br>
The professional speaker always starts by finding out as much as possible about 
the audience, their reasons for being present, and their motivating factors. 
This can be done by talking to the meeting organiser, but is better dealt with 
by talking directly to prospective audience members themselves. <br>
Once a speaker understands their audience, it is then time to decide on the key 
message to be delivered. Another common error is to try to impart too much 
information. <br>
It is essential that the core message of any speech can be summarised in one 
sentence of around twenty words, adds Discouraging as this may seem, if an 
audience member is asked the day after hearing a speech “What was the speech 
about?”, and they can remember the core message, the speech will have been a 
great success. Thirdly, speakers need to remember that they are not just 
deliverers of information. There is a story from Ancient Greece about a speaking 
contest between two great orators. At the end of the first speech, the audience 
rose and cheered the speaker, calling out “What a great speech”. At the end of 
the second speech, the audience rose and shouted “Let’s march on Sparta!”. <br>
The hallmark of a truly great and professional speech is not changing a person’s 
point of view, but changing their behaviour. Lastly, speakers need to consider 
the way in which they present. There are no absolute rights and wrongs here, but 
experts agree that there are some things you should never do, such as –<br>
1. Start badly<br>
2. Fail to understand equipment<br>
3. Put too much on each slide<br>
4. Patronise the audience<br>
5. Use bad graphics<br>
6. Turn their back on the audience<br>
7. Speak inaudibly<br>
8. Use jargon<br>
9. Run out of time<br>
10. End poorly</font></p>

