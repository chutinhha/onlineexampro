<p align="justify"><font face="Verdana" size="2" color="#C0C0C0">:: Marketing</font></p>

<p align="justify"><font face="Verdana" size="4" color="#FF0000">Here’s looking 
to 2005</font><font face="Verdana" size="1"><br>
<br>
<b>Matt Harrison provides a few pithy pointers for marketing to the millennium 
generation in the time to come</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im86.jpg" align="left" width="200" height="130">Times are 
changing and a new generation of people are driving our economy. These are the 
sons and daughters of Baby Boomers – born during a baby bulge that demographers 
locate between 1979 and 1994, they’re as young as 10 and as old as 25. They go 
by a host of taglines: Generation Y, Echo Boomers, or Millennium Generation. 
With new ideas and values based solely on the core values of the emerging 
generation, here are a couple of things I expect to see happen over the next 3 
to 4 years: </font></p>

                    <ul>
                      <li>

<p align="justify"><font face="Verdana" size="1">A decline among prestige 
brands. For example, PepsiCo Inc. has struggled to build loyalty among teens. 
Nike Inc.’s sneaker sales are tumbling as the brand sinks in teen popularity 
polls. Levi Strauss &amp; Co., no longer the hippest jean maker on the shelf, is 
battling market share erosion. Meanwhile, newcomers in entertainment, sports 
equipment and fashion have become hot names.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">A decline in the effectiveness 
of traditional advertising. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Comparison-shopping to be done 
increasingly online, though purchasing will remain in brick-and-mortar 
businesses in many product categories. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">An increase in volunteerism and 
donor support to socially responsible organisations. </font></p>

                      </li>
</ul>

<p align="justify"><font face="Verdana" size="1"><b>What does this mean to your 
business?</b></font></p>

                    <ul>
                      <li>

<p align="justify"><font face="Verdana" size="1">Don’t count on prestige brands 
to be the strength of your identity in the future as they have been in the past. 
Craft your own identity that doesn’t depend on the vendor lines you carry. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Investigate the values of the 
new ‘upscale’ customer of tomorrow and get in step with those new values. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Focus less on instant-response 
and become the business customers think of immediately when they need what you 
sell. The new trend in advertising is away from hype and romanticism and more 
toward facts and truth. Moreover, the continuing fragmentation of mass media 
means your ability to access public awareness will continue to diminish. In the 
future, customer referrals—viral marketing—will become more important than it 
has ever been. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Have an informative, 
user-friendly Website that allows customers to learn all the details of your 
product or service they would have traditionally learned from a salesperson. 
Don’t expect tomorrow’s customer to call you or visit your business ‘‘for more 
information.’’ </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Pick a charitable cause you 
believe in and support it openly. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Speak less often to self-image 
and prestige as a motivating force in your customer. Believe it or not, people 
are becoming concerned about a world outside themselves. </font></p>

                      </li>
</ul>

<p align="justify"><font face="Verdana" size="1">Given these changes and the 
fact we’re fast approaching the end of the year, this month’s column looks at 
marketing ideas for 2005. The objective is simple: offer OER readers marketing 
inspiration to re-energise their businesses and meet the changes we’ll face 
ahead. In brief, to welcome in the New Year with a marketing bang! Some of the 
ideas are better than others and some are easier to pull off than others, but 
it’s a list of creative ways you can promote your business in 2005. Indeed, 
doing just one of these promotions will give you experience and certainly some 
very valuable publicity. Here we go:</font></p>

                    <ul type="square">
                      <li>

<p align="justify"><font face="Verdana" size="1">Send out your new business card 
to potential clients or customers. On the back, hand-write a note offering a 
free consultation or sample product to get them acquainted with your business. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Hold a weekly raffle on your 
Website. When visitors opt in, they’re assigned a virtual raffle ticket. You 
pick a ticket each week and the winner gets a free fridge magnet, mouse pad, 
frisbee, baseball cap or other promotional product printed with your business 
name, logo and URL. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Brainstorm with employees, 
family and friends. Make it fun – serve food and drinks and tell people there 
are no bad ideas. Encourage them to think outside the box. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Publish a newsletter for 
customers and prospects.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Create a ‘signature file’ to be 
used for all your e-mail messages. It should contain contact details including 
your Website address and key information about your company that will make the 
reader want to contact you.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Announce free or special offers 
in your direct mail (this may be via post, broadcast fax, or e-mail messages). 
Include the offer in the beginning of the message and also on the outside of the 
envelope for direct mail.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Use an answering machine or 
voice mail system to catch after-hours phone calls. Include basic information in 
your outgoing message such a business hours, location, URL, etc.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Turn your Web banner ad into a 
trivia question. Post the question on the banner and tell readers they can win a 
prize if they answer the question at your Website.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">If you’re looking to develop a 
popular discussion board on your company’s Website, give away a free product or 
service to any person that posts 10 or more messages on your discussion board in 
a month. It could be anything from a free book, a business report to movie 
tickets. Just keep track of everyone’s postings each month. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">For those in the hotel, coffee 
shop or restaurant trade, get taxi drivers interested in your business. Take 
some cab rides to your business, talk to the driver about it and give him a free 
sample. He’ll be sure to remember your name when passengers ask him for a good 
restaurant, coffee shop, or hotel.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Create a promo kit you can give 
or (e-)mail to prospects and the media that contains all or some of the 
following: photos of you and your team; articles written about your business; 
your corporate brochure; CD; a list of questions you’re frequently asked about 
your business and the answers to those questions; and anything else that lets 
people know who you are and how your business can help them.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Join a Networking Group. These 
are regular meetings where people exchange contacts and referrals. There are 
plenty of these held in Oman – Knowledge Oasis Muscat’s Open House Program is an 
example.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Make sure you know who’s raving 
about you. These are your advocates. They may be friends, clients, or 
associates. These people are your best referral resources. Your advocates will 
send you business simply because they believe in you. Make sure you recognise 
them as such. Never let more than 30 days go by without staying in touch with 
each of your advocates.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Contact the media and tell them 
your expertise. Tell them you’re willing to be a resource if they’re ever 
writing a story on your special area. Stay in touch with media contacts and you 
may get a call for a quote or interview. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Ask your clients for referrals. 
This is a simple, yet powerful way to meet new prospects. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Host a fundraising event, this 
is a way to give back to your community and meet some wonderful people at the 
same time. You could organise a race, a blood donation drive, a sponsored walk 
or run or a good old-fashioned raffle, just about anything you can think of to 
raise money for a good cause. You not only give back, but you also create more 
awareness about your company. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Create a direct mail list of 
‘hot prospects.’</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Create a poster or calendar to 
give away to customers and prospects.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Reward return business. The 
first time a client or customer comes back with more business for you, offer a 5 
per cent discount; the second time, a 10 per cent discount. Be sure to promote 
your discounting program up front.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Sponsor a sports team. This can 
be as sophisticated as you want. For example, sponsor a football team that plays 
afternoon games on Al Shatti Beach. Alternatively, splash out and sponsor one of 
the Sultanate’s national teams, swimming (hats and towels); athletics 
(tracksuits); tennis (shirts). </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Have a joint venture with 
another business and display each other’s products in your respective showroom 
floors/offices. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Sponsor special sales for 
specific communities (niches) of people who give to the community such as: 
nurses, doctors, teachers and policemen. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Depending on your available 
space, buy or rent a trailer and post a huge sign on the side of the trailer and 
let it sit in front of your business or move it around town. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Positive picketers who picket 
your business with signs that say how good your business is. If you employ this 
tactic, call the press and you’ll get yards of coverage. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Give your customers special 
incentives to put a bumper sticker on their cars. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Host or Sponsor an Award. For 
example, the best teacher award and the best student award. Just contact one of 
your local schools and ask them for submissions. Give away prizes to the teacher 
and a book token to the student. This affiliates you with a good cause and you 
get lots of free publicity.</font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">Host a ‘Free Seminar.’ Team up 
with other businesses or ministries to host a free seminar on your area of 
expertise. </font></p>

                      </li>
                      <li>

<p align="justify"><font face="Verdana" size="1">I’ll end on a cheeky note. Play 
Dirty: During your competition’s big promotion, rent a billboard across the 
street from them with a huge ad for your business.</font></p>

                      </li>
</ul>

