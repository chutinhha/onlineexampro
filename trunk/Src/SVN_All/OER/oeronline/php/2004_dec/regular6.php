            Professionally, the major attraction in Oman is that despite being a 
            relatively smaller world, it has all the challenges and complexities 
            that one could find in a larger environment. The exposure to these 
            diverse facets of operations that one can gain in a smaller 
            environment will not be available in a larger environment as in 
            India. To be successful, one needs to have the ability to wear 
            multiple hats at the same time and react speedily to the various 
            complex situations and challenges that the dynamic business 
            environment throws up.<br>
            <br>
            <b>Briefly describe your management style.</b><br>
            I believe in the consultative management style while at the same 
            time being decisive. I have absolute faith in my management team and 
            staff and believe in having their participation in the 
            decision-making process. <br>
            <br>
            <b>How is the insurance sector shaping up in Oman?</b><br>
            Oman has always remained a highly competitive market in view of the 
            number of players relative to the size of the market. Due to the low 
            annual growth rate of market premium and due to the annual cycle of 
            business placements, competition among the local players will 
            continue to remain heated. <br>
            Recent events—both locally and internationally—have resulted in a 
            review of the insurance regulations and business practices of 
            companies in Oman. The shifting of the Insurance Department from the 
            Ministry of Commerce &amp; Industry to the Capital Market Authority 
            (CMA) is intended to achieve closer monitoring by the CMA and 
            implementation of the best practices in the insurance sector. The 
            increase in earning levels of individuals, higher savings propensity 
            and higher insurance awareness holds the potential for growth in 
            retail insurance products. With the Central Bank of Oman approving 
            Bancassurance as an activity for the banking sector, the banks will 
            emerge as a major intermediary in the sector.<br>
