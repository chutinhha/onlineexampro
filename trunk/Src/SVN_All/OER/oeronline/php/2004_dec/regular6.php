            <b>Please enlighten us about the management structure at Al Ahlia 
            Insurance…</b><br>
            At Al Ahlia, we have a close-knit management team with clearly 
            defined authority levels. The company believes in delegating 
            authority level commensurate with the operational requirements and 
            complement the same with a good reporting system. There are two 
            assistant general managers to support the general manager. The 
            company has a customer-centric organisation structure. To avoid 
            conflict of interest, the business centres are broadly structured as 
            Direct Insurance Department and Agencies &amp; Brokers Department. 
            Within these departments, there are assistant managers and account 
            executives that have been assigned specific client locations. The 
            clients, agents and brokers, therefore, have a single contact point 
            for all their requirements.<br>
