<p align="justify"><font face="Verdana" size="4" color="#FF0000">Easy Rider</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Saubhagya Mohan Kala talks 
to the young and suave HH Faisal bin Turki Al-Said, head of Planning and 
International Relations, OCIPED, about the challenges on the work front and his 
love for bikes</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="profile.jpg" align="left" hspace="5" width="200" height="266">A motorbike enthusiast who 
loves to tinker with electronics and is an avid traveller (London, Paris, 
Germany, Austria, Lebanon, Kuwait, Saudi Arabia, Malaysia, Egypt and counting), 
HH Faisal bin Turki Al-Said, head of Planning and International Relations at 
OCIPED, was born in Lebanon and brought up in Oman in Muscat.<br>
‘‘You might as well ask why Lebanon? Well, my father was in the country for a 
few months taking courses in law and related aspects. I was born on July 23, 
1973. My father used to work in the emigration department back in the 60s, and 
later he headed the regional municipalities in Muscat in the 70s. After that, he 
became the president of the Criminal Court in Oman in the 80s for about 10 
years. Currently, he is the State advisor for Criminal Affairs.” <br>
A proud father of two sons—Shihab, aged 8, and Sultan, who turns 6 in 
January—Faisal bin Turki recounts how his childhood was dominated by motorbikes, 
travelling and electronics. “In fact, it is my dream to own a Harley Davidson 
and if I can get a good deal, I will certainly purchase it.” An electronics 
aficionado, Faisal bin Turki is a keen photographer who loves to be left alone 
with his digital camera to shoot and is also into computers. ‘‘I like reading 
books on computers, electronic gadgets and the history of Oman in particular.’’<br>
<br>
<b>Career path</b><br>
Faisal bin Turki did Business Administration from the UAE University, Al Ain, in 
1996 and began his career in 1997 at OCIPED. “Back then, OCIPED was newly 
established and the team consisted of not more then 13 to 15 employees in total 
(now 32). My initial role was not clear to me since it was a new organisation. I 
was involved in different small projects and was responsible for a lot of the 
marketing activities since the person who was in charge of the marketing was 
away during that period.” <br>
Faisal bin Turki, after a year, became responsible for the Planning and 
Follow-up department and that is where he worked for five years, dealing with 
the two main directorates in OCIPED – the Investment Promotion and the Export 
Development. The exposure that he got in these five years was more in terms of 
getting to know about almost all the activities for the two departments as well 
being involved in the annual planning and strategy for OCIPED including the 
5-year plan.<br>
<br>
<b>Meeting challenges</b><br>
“The main challenge during the start of my career at OCIPED was getting to 
understand the nature of the work and that was not easy since I did not really 
know what exactly would OCIPED’s role be as it was a new organisation. Being 
involved and responsible for major events of an international calibre right at 
the start of my career was a big challenge as well (Oman Economic Forum 
1997/1998), but I did learn a lot from that experience.”<br>
Because of a small team at OCIPED, Faisal bin Turki faced yet another challenge 
– to work alone on various projects and always try very hard to learn from 
others whenever there was any time to breath. “But our energy levels and 
eagerness kept us going and we overcome the challenges. And I believe that goes 
for most of my colleagues.”<br>
<br>
<b>Leading by delegation</b><br>
A leader who believes in delegation of work, Faisal bin Turki says, “I strongly 
believe in delegating tasks. I practice that on a daily basis with my team and 
it has worked wonders. That does not discount the hands-on approach totally. 
That will still be required depending on the situation and the tasks at the 
initial stage. <br>
But delegating tasks helps build confidence in my team and brings out the best 
in each one of them. When facing a problem, I would rather ask ‘How do YOU think 
we should tackle this?’ rather than tell them that this is what I think should 
be done.’’<br>
<br>
<b>Human element</b><br>
A man with his heart in the right place, Faisal bin Turki’s management 
philosophy is employee-centric. “Employees are an essential part of the 
organisation without which it cannot survive. The human element is the heart of 
any concern, and the more you invest in it, the more it pays you back. My 
subordinates are my team, and the closer I understand each one of them, the 
better mileage I get from each member.” <br>
Faisal bin Turki also gives due importance to business partners and customers. 
“With business partners, we gain new strengths. Managing customer expectations 
is an art and a challenge for every organisation. In fact all three (employees, 
stakeholders and customers) are essential for the survival of an organisation.’’<br>
<br>
<b>On OCIPED</b><br>
OCIPED was established to promote Oman as an exclusive and distinctive 
destination to consider for investment and a base for international business. 
According to Faisal bin Turki, this has been possible through the conducting 
road shows, conferences and seminars both international and local. ‘‘A lot of 
focus and emphasis has been put to highlight non-oil sectors with the aim to 
diversify. Sectors such as tourism, industry, fisheries, agriculture and IT have 
been touched and looked into carefully. I personally feel there is still more 
that can be done for tourism and IT and the various other sectors. Oman has 
still much more to offer to prospective investors seeking golden opportunities.”<br>
<br>
<b>Privatisation spree</b><br>
According to Faisal bin Turki, the air of privatisation pervading Oman at 
present is a very healthy sign for the economy. “Oman has realised that it’s 
time that the private sector plays a leading role in the development of this 
country.” It is true that Oman is determined to include its private sector in 
the vast opportunities to further develop the country and significantly reduce 
the dependency on oil along with the other non-oil sectors. <br>
“The private sector has realised the opportunity and has been cooperative and 
supportive, and this has been the element that has made this government 
initiative a success in many sectors,” he adds. <br>
<br>
<b>Promising future for Oman </b><br>
Oman is today moving towards its goal of being a non-oil dependant economy and 
Faisal bin Turki is very excited about what the future holds for the Sultanate. 
“I personally think Oman has done a lot and is willing to do even more when it 
comes to diversifying and minimising the dependency on Oil. The government has 
realised a long time ago that oil will not last forever and something has to be 
done about it. The fluctuating oil prices have as well played a role in 
realising this challenge. In this context Vision 2020 has been put in place with 
the aim of achieving the ultimate objective of diversification. This strategy is 
continuously being reviewed and assessed by the concerned authorities. <br>
Looking at the trend of the oil prices for the past year or so Oman has captured 
the opportunity that the rise in oil prices had offered and invested more in the 
development of the infrastructure and the various non oil sectors as well.”<br>
According to Faisal bin Turki, there is definitely more room for improvement and 
more has yet to happen. “More and more major projects are being introduced in 
Oman proving that Oman is considered to be a secure destination for foreign 
investments and provides vast opportunities for serious investment all this of 
course under the wise leadership of His Majesty the Sultan Qaboos.” <br>
<br>
<b>Personal front</b><br>
A family man at the core of his heart, Faisal bin Turki loves to spend quality 
time with his family. “My family is also my circle of close friends. I love my 
kids very much and I always hope I will be able to provide them with the best of 
care, love and attention. I also like sitting with the elderly and learning more 
about life from them.”&nbsp; </font></p>

