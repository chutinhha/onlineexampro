<b>Bahrain simplifies immigration process with e-visa </b><br>
<img border="0" src="im82.jpg" align="left" hspace="5" width="200" height="155">Streamlined visa processes in Arabia are essential to stimulate visitor figures, 
according to one government tourism official, who said that online visa 
application facilities have proved highly successful since their recent 
introduction. Bader Nasser, head of marketing for tourism affairs for the 
government of Bahrain, highlighted the Kingdom’s e-visa initiative as a 
“necessary measure” if the drive for fast and seamless immigration processes for 
business and leisure travellers is to be met.<br>
The Bahrain e-visa project—a first for the Middle East and one of only a handful 
in the world—comes as the topic of visa restrictions comes under the spotlight 
at the first-ever Arabian Hotel Investment Conference, to be held in Dubai on 
April 30 – May 2, 2005. Bahrain was the first country in the Middle East and one 
of the only countries in the world to issue online visit visas to simplify—and 
‘fast track’—its immigration processes. To date, an average of 1,500 visas are 
downloaded a month, with numbers expected to double during the next 12 months. 
The e-visa initiative is the first phase of a drive to move all of Bahrain’s 
immigration processes online. Nasser said: “For too long, the issue of visas in 
Arabia has blighted the attempts of the tourism authorities especially to 
stimulate visitor numbers. We are pioneering the switch to e-visas as part of 
our strategy to become a tourism destination of the future.”<br>
Residents of 33 countries outside the GCC can apply online at www.evisa.gov.bh, 
allowing them to download their visit visa anywhere in the world before 
travelling to the Kingdom. Those able to take advantage of these facilities 
include UK, US, Australia and European passport-holders. Nasser said: “While the 
ideal scenario is still a single visa for Arabia for travel between the Gulf 
states, we have to look at new ways to make the immigration process as simple as 
possible for visitors.”</font></p>

