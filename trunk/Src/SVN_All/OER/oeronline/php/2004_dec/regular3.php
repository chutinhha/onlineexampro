<b>Cellucom launches first showroom in Kuwait</b><br>
Cellucom, one of the premier distributors of mobile communication devices and 
accessories in the Middle East, opened its first showroom in Kuwait last month. 
The new showroom is located at the Saha Complex at Salem Al Mubarak Street, 
Salmiya. The launch is part of Cellucom’s plans to expand its operations further 
into the GCC region. The company already has a network of high-end communication 
stores in UAE, Bahrain, Qatar and Saudi Arabia.<br>
Arun Nagar, managing director, Cellucom, said: “We are extremely proud to launch 
our operations in Kuwait. The country has one of the Arab World’s most advanced 
telecommunications markets and the mobile phone market in Kuwait has been 
witnessing significant growth in recent years. Revenues from cellular 
connections were more than double the revenues from Public Switched Telephone 
Network in 2003. This provides Cellucom an excellent footing into a lucrative 
market and strengthens our operation in the region.”<br>
The telecommunication penetration rate in Kuwait in 2004 has risen to more than 
80 per cent. According to projections, Kuwait’s cellular subscriber base is 
expected to witness 5 per cent growth between 2003 and 2008 to reach around 2.17 
million subscribers by end-2008. Mobile phone revenues are projected to touch 
the $1 billion (RO384.62 million)-mark in 2008.</font></p>

