				shallow breathing, the chest does not expand as much as it would 
				with slower deeper breaths and much of the air exchange occurs 
				at the top of the lung tissue. This results in “chest” 
				breathing. You can see if you are a chest breather by placing 
				your right hand on your chest and your left hand on your 
				abdomen. As you breathe, see which hand rises more. If your 
