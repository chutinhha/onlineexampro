				<b>Relieving stress </b><br>
				During times of emotional stress our sympathetic nervous system 
				is stimulated and effects a number of physical responses. Our 
				heart rate rises, we perspire, our muscles get tense and our 
				breathing becomes rapid and shallow. If this process happens 
				over a long period of time, the nervous system becomes over 
				stimulated leading to an imbalance that can effect our physical 
				health resulting in inflammation, high blood pressure and muscle 
				pain to name a few. Consciously slowing our heart rate, 
				decreasing perspiration and relaxing muscles is more difficult 
				than simply slowing and deepening breathing. The breath can be 
				used to directly influence these stressful changes causing a 
				direct stimulation of the parasympathetic nervous system 
				resulting in relaxation and a reversal of the changes seen with 
				the stimulation of the sympathetic nervous system. We can see 
				how our bodies do this naturally when we take a deep breath or 
				sigh when a stress is relieved.<br>
