                    shied away from the gallery and sometimes couldn’t eat during 
                    competition time. Most people would imagine that such achievements 
                    would have necessitated a full time commitment to practice. 
                    However, Jones only devoted three months of every year to 
                    golf. The remaining time was spent acquiring first class honours 
                    degrees in Law, English Literature and Mechanical Engineering 
                    - all from different universities. When he retired he set 
                    up his own law practice in Atlanta.<br>
                    <br>
                    Jones continued his involvement in golf after retiring. He 
                    made instructional films and contributed articles to golf 
                    magazine<img src="images/img121.jpg" hspace="8" vspace="4" border="0" align="right">s. 
