                    in collaboration. However Jones cites playing the old course 
                    St Andrews as his most rewarding golfing experience. In recognition 
                    of his contribution to golf and the Open Championship, the 
                    R&amp;A awarded him honorary membership in 1956. He was later 
                    made a freeman of St Andrews.<br>
                    <br>
                    Unfortunately in 1958, Jones became wheelchair bound due a 
                    spinal disease. When he died, a memorial service was held 
                    in his honour at St Andrews. His portrait hangs proudly in 
                    the R&amp;A clubhouse. The USGA Museum has a room dedicated 
                    to his memory.<br>
