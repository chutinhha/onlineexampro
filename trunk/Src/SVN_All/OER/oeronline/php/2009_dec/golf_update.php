                    The young Jones played there from the age of five and soon 
                    demonstrated a natural talent. He became junior club champion 
                    when he was just nine. He never had a formal lesson yet his 
                    swing was so smooth and powerful that professionals would 
                    later emulate it.<br>
                    <br>
                    At the turn of the century, the Majors were considered to 
                    be the British and US Opens and Amateur Championships. Jones 
                    played his first major tournament, the US Amateur, when he 
                    was 14 and finished in the top 10. However, it was not until 
                    1923 that he won his first Major, the US Open. By the time 
                    he retired from competitive golf in 1930, Jones won the Open 
                    three times, the British Amateur once, the US Open four times 
                    and the US Amateur four times. He is the only golfer to have 
                    won all the Majors in a single year in 1930. In all he won 
                    13 national championships in eight years. In addition he played 
                    in every Walker Cup since it’s inauguration in 1922 until 
                    retirement.<br>
