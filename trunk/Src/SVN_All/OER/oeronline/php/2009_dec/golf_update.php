                    Jones won everything in golf and then he won it again. His 
                    career was short yet hugely successful. No one has achieved 
                    so much in such a short career. On that basis, he can be considered 
                    the greatest golfer ever. In addition, he competed only as 
                    an amateur and therefore demonstrated an unadulterated love 
                    for the game. Even today he sets the standard by which the 
                    greats are judged. Bobby Jones was among the first class of 
                    inductees into the World Golf Hall of Fame in 1974. <br>
