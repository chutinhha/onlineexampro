		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2">Good Regulatory Regime</font></b><font face="Verdana" size="2"> </font><font face="Verdana" size="1"> 
                    <br>
                    <b>It Is The Capital Market Authority’s Mission To Continue 
                    To Enhance The Efficiency And Transparency Of The Financial 
                    Market In The Sultanate Of Oman</b> <strong>by Dr Caroline 
                    Bolle</strong><br>
                    <br>
                    <b><img src="images/img114.jpg" width="97" height="105" hspace="9" vspace="5" align="right"></b>The financial market in the Sultanate of Oman has 
                    developed substantially since the inception of the first development 
                    plan by His Majesty Sultan Qaboos bin<b></b> Said Al Said. 
                    Aimed at, amongst other things, globalising the economy, the 
                    development plan has resulted in the creation of a sophisticated 
                    securities market, the Muscat Securities Market (“MSM”), supervised 
                    by the Capital Market Authority (“CMA”).<br>
                    <br>
                    <b><font color="#CC3333">The Muscat Securities Market and 
                    the Capital Market Authority: two independent authorities 
                    with distinct functions</font></b><br>
                    The MSM was set up pursuant to Royal Decree 53/88 (issued 
                    on 21 June 1988). Initially the regulatory and operational 
                    activities were both assumed by the MSM, but a decade after 
                    its formation the financial market was reformed to separate 
                    the regulatory function from the day-to-day operational activities 
                    of the market. Under the current regime, the operation of 
                    the stock market continues to be entrusted to the MSM, whereas 
                    the regulatory function has been allocate</font><font face="Verdana" size="1">d 
                    to the CMA since January 1999 by virtue of Royal Decree 80/98 
                    issued on November 9, 1998. This brings the organisational 
                    structure of the Omani financial market in line with international 
                    standards.<br>
                    <br>
                    The CMA is set up as an independent body and exercises its 
                    powers through executive regulations or administrative decision</font><font face="Verdana" size="1">s. 
                    It is the CMA’s mission to continue to enhance the efficiency 
                    and transparency of the financial market in the Sultanate 
                    of Oman</font><b></b><font face="Verdana" size="1">. The CMA 
                    appreciates that ensuring continued market efficiency and 
                    transparency is crucial in order to attract new funds to the 
                    Sultanate of Oman.<br>
                    <br>
                    The MSM has maintained its status as an independent body. 
                    The MSM stock exchange is the only regulated market for trading 
                    securities in the Sultanate of Oman. The principal stock index 
                    applied by the MSM is the MSM-30</font><font face="Verdana" size="1">. 
                    The latter was established in 1992 and is composed of 30 companies 
                    selected from across all industries. The companies constituting 
                    the MSM-30 are well-performing companies with substantial 
                    market capitalisation and high liquidity. In addition to the 
                    MSM-30, three further industry-focused indexes are used by 
                    the MSM, including the Banks &amp; Investment Index, the Industry 
                    Index and the Service &amp; Insurance Index. Each industry 
                    index comprises the top 30 companies in their relevant sector. 
                    Modernisation of the securities market has continued and in 
                    early 2008 the MSM partnered with Dow Jones to launch the 
                    Dow Jones MSM Index. The Dow Jones MSM Index is an indicator 
                    of the performance of financial products (such as exchange 
                    traded funds and other investable products) that enables investors 
                    to participate in the performance of the Omani stock market. 
                    In August 2009 new indexes were launched in cooperation with 
                    Dow Jones Indexes measuring the performance of stocks listed 
                    in the Gulf Cooperation Council in which shares listed on 
                    the MSM are included.<br>
                    <br>
                    <font color="#CC3333"><b>The MSM boasts an active market for 
                    initial public offerings and rights issues</b></font><br>
                    The term initial public offering is typically used when a 
                    company first issues shares to the public. Rights issues then 
                    occur when an already listed company issues additional securities 
                    to its shareholders. The market for initial public offerings 
                    and rights issues in the Sultanate of Oman has seen some notable 
                    transactions and to date about 234 companies and 8 mutual 
                    funds have been admitted to listing on the MSM. There have 
                    also been 8 commercial bond issuances and 27 government bond 
                    issuances.<br>
                    <br>
                    <font color="#CC3333"><b>The requirement to publish a prospectus 
                    approved by the Capital Market Authority</b></font><br>
                    In all instances where securities are offered for public or 
                    private subscription a prospectus approved by the CMA must 
                    be published pursuant to the Capital Market Law promulgated 
                    by Royal Decree 80/98 as amended from time-to-time (“CML”). 
                    A prospectus is an often lengthy document in which the issuer 
                    provides written disclosures. By disclosing all relevant information 
                    the issuer not only informs the prospective purchaser about 
                    the investment opportunity but also produces a record of all 
                    information disseminated to the public in connection with 
                    the issue that may become relevant if in the future disputes 
                    arise between the company and a dissatisfied investor.</font><font face="Verdana" size="1"><br>
                    <br>
                    </font><font face="Verdana" size="1">A prospectus must contain 
                    all material information that may assist potential investors 
                    to make an informed decision about whether or not to invest 
                    in the issuing company. Unlike the purchase of a tangible 
                    asset such as a house or a car (which a potential buyer can 
                    physically inspect), a security is an intangible asset the 
                    value of which depends on the performance of the company concerned 
                    and the assessment by the market of its prospects. It is with 
                    this in mind that securities laws require the issuer to inform 
                    prospective investors of all material information about the 
                    company to allow them to make an informed investment decision 
                    with full knowledge of all material issues relating to the 
                    value of the security and the risks present.<br>
                    <br>
                    Preparation of the prospectus is perhaps the most important 
                    aspect of conducting a proper securities offering. Without 
                    doubt it demands a substantial commitment on the part of the 
                    company’s management to provide the requisite information 
                    for inclusion in the prospectus within the timeframes set 
                    by the CMA. However, the prospectus is ultimately the key 
                    marketing document that will attract shareholders to invest 
                    funds in the business and it may also become the main reference 
                    document to defend against potential litigation if in future 
                    a disgruntled investor initiates legal proceedings against 
                    the company. <br>
                    <br>
                    What information is material and needs to be disclosed in 
                    the prospectus depends on and varies according to the specifics 
                    of each company and the circumstances surrounding the proposed 
                    securities offering. The administrative regulations of the 
                    CMA shed some light on the issue by prescribing a detailed 
                    list of minimum information that must be included in the prospectus 
                    (see Administrative Decision 12/2005 issued on 31 December 
                    2005). <br>
                    <br>
                    <font color="#CC3333"><b>A prospectus must be published upon 
                    the public or private issuance of securities</b></font><br>
                    Having discussed the contents of the prospectus above, it 
                    is worth looking beyond the definitions of “Securities”, “Public 
                    Subscription” and “Private Subscription” in the CML to assess 
                    the full scope of the CML and the reach of the CMA.<br>
                    <br>
                    If in doubt, an issuer should always contact the CMA to discuss 
                    whether an offer is indeed an offer to the public or an invitation 
                    to a specified category subject to the prospectus requirements. 
