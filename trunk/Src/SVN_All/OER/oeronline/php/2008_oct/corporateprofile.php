            <td width="492" bgcolor="#FFFFFF" valign="top"> <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <br>
                </font><b><font face="Verdana" size="4">A strategic shift</font></b><font face="Verdana" size="1"><br>
                <b>National Gas Company is repositioning itself as an energy company 
                which provides turnkey solutions. Mayank Singh reports</b><br>
                <br>
                <img src="corp2.jpg" width="150" height="155" hspace="8" vspace="6" align="right">National 
                Gas Company (NGC) is undergoing a subtle shift. The company which 
                has conventionally been seen as an LPG supplier is reinventing 
                itself as an energy player. The share of the LPG business in the 
                company’s turnover is expected to come down from 50 per cent in 
                2008 to anywhere between 10-15 per cent over the next five years, 
                the remaining being contributed by new verticals. Says Sheikh 
                Khalid Ahmed Alhosni, Chairman, NGC, “The company has reinvented 
                itself as a pioneering gas marketing company rather than being 
                an LPG bottling company.”<br>
                <br>
                The company set up a subsidiary called Innovative Energy Systems 
                (INERSYS) which aims to tap the gas engineering market. INERSYS 
                has introduced several innovations such as the first township 
                piped gas network in Oman and the first synthetic natural gas 
                system. The company is working on several other initiatives that 
                are in the process of being finalised and the full impact of these 
                is expected to be felt in 2009. <br>
                <br>
                <strong>New ventures</strong><br>
                <img src="corp3.jpg" width="250" height="269" hspace="8" vspace="6" align="left">INERSYS 
                will provide turnkey engineering solutions in the energy field 
                (excluding oil and gas). These include supply of equipment, installation 
                of network and grids, civil work, operational maintenance etc. 
                While the LPG business will continue to grow at a steady pace 
                of around three per cent (in line with the growth in population) 
                new businesses are expected to give better returns for the company. 
                Says Alhosni, “We are trying to reduce dependence on the bottled 
                LPG business and produce long term sustainable business potential 
                for NGC.”<br>
                <br>
                The INERSYS impact is already visible in the company’s numbers. 
                NGC’s revenues have grown by over 100 per cent year on year during 
                the second quarter of 2008 while its operating profits have increased 
                by 400 per cent. Says Alhosni, “This is a result of moving into 
                new business lines and reducing low margin cylinder business income 
                from 90 per cent to 50 per cent of total revenues. We will continue 
                to foster this strategy to bring cylinder income to 10-15 per 
                cent of the total by enhancing presence in new areas that bring 
                radically superior technology and service levels to benefit our 
                customers.” The growth of the company finds resonance in the share 
                price of company – NGC’s shares which were trading at RO2.233 
                in 2006 shot upto RO6.223 in August 2008.<br>
                <br>
                <strong>Business model</strong><br>
                NGC started commercial operations in 1981 and it has spread across 
                the country with eight strategically located plants. The parent 
                plant at Rusayl is spread over 24,000 sq mtr area and has been 
                serving the national capital area since its inception. NGC with 
                its eight plants, efficient tanker fleet and trained personnel 
                is the largest LPG supplier in the sultanate serving domestic, 
                commercial and industrial consumers. The company has a share capital 
                of two million rials. <br>
                <br>
                Its strong distributor network ensures timely delivery of gas 
                to households and commercial establishments. In the LPG space 
                it bottles two variants -- the 22 kgs cylinder that is popular 
                with households and the 44 kgs cylinder that is used in commercial 
                establishments such as restaurants, hospitals, kitchens and industries. 
                <br>
                <br>
                Apart from this, LPG is also distributed in bulk to high volume 
                consumers such as hotels, hospitals, defence establishments, industries 
                and labour camps etc. NGC has the expertise to design, install 
                and operate a bulk gas system for its customers. The growing number 
                of installations, buildings, townships and industries is helping 
                NGC’s bulk business to grow at a rapid rate. It sees this as a 
                major contributor to NGC’s bottomline in the future. Says Goutam 
                Sen, general manager, NGC, “The progress till date has been encouraging 
                and we are perfectly poised to exploit the large potential in 
                bulk gas expected in the upcoming period.”<br>
                <br>
                The company has also ventured into piped gas. NGC is executing 
                the largest piped gas network in Oman which includes about ten 
                kilometers of buried pipeline. The company believes that property 
                developers will see the benefits afforded by such systems and 
                thereby select to install piped networks instead of cylinders 
                for their tourist resorts, residential complexes and hotels in 
                future. The company has got an encouraging response from several 
                new projects. NGC dominates the market with a market share of 
                over 50 per cent in Oman. It has also managed to corner a 25-30 
                per cent share of the Northern UAE LPG market through export tenders. 
                The latter constitutes a significant part of its revenues.<br>
                <br>
                <strong>Stumbling blocks </strong><br>
                <img src="corp.jpg" width="166" height="215" hspace="8" vspace="6" align="right">While 
                NGC has seen promising growth it is faced with its own set of 
                constraints. Says Alhosni, “Our growth is majorly inhibited by 
                restrictive regulations that constrain us through quotas and differential 
                pricing strictures. This is a major obstacle to harnessing the 
                ecological and environment friendly properties of LPG to develop 
                local demand.” The government gives each company a pre-determined 
                quota for LPG and they cannot sell more than their prescribed 
                limit.<br>
                <br>
                The company has taken up the matter with concerned authorities 
                and is hopeful that their concerns would be addressed soon. The 
                government has issued licenses to 12 players and more are expected. 
                The company claims that a number of these licenses have been issued 
                to people who do have the capacity to service them and invariably 
                end up selling their quota to established companies. Given such 
                market constraints NGC is not planning any major investments in 
                new capacities in the cylinder business right now.<br>
                <br>
                NGC has also made a foray into the aerosol propellant business 
                and supplies deodorised LPG to industries manufacturing perfumes, 
                deodorants, pesticides, etc – as an environment friendly alternative 
                to ozone depleting CFC’s. “Aerosol Propellant is a niche market 
                and while it is a profitable business, its impact on the bottomline 
                is minimal due to low volumes. However, it is good product to 
                have in our range so that we can service an entire gamut of customers 
                in the field,” says Alhosni.
                The company wants customers to be a bit more aware and demanding 
                when it comes to the service that distributors provide them. “Since 
                we do not have the tools to see that the customer gets the same 
                service as we provide at our plant we want customers to look for 
                suppliers who are consistent,” says Alhosni.<br>
                <br>
                NGC insists that the initiatives taken till now constitute a mere 
                15 per cent of its overall plans. The remaining blueprint is to 
                be rolled out over the next three to five years. With such ambitious 
                plans both customers and competitors are sure to keep a close 
                eye on this energy player in the making. </font></p>
