                <font face="Verdana" size="4">Higher prices offsets decline</font></b><font face="Verdana" size="1"><b><br />
                    </b><br />
                    According to Central Bank of Oman’s Annual Report 2007, oil 
                    and gas activities continued to dominate the economic performance 
                    of Oman in 2007, accounting for about 45.3 per cent of nominal 
                    GDP, 62.1 per cent of net fiscal revenue and 58.4 per cent 
                    of merchandise exports. The
                    indirect contribution of the oil and gas sector in the form 
                    of oil and gas-based industrialisation has also increased 
                    considerably in recent years.</font></p>
                  <p align="center"><font size="1" face="Verdana"><img src="numbers1.jpg" width="292" height="163" /></font></p>
                  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Despite 
                    the sustained progress of economic diversification and the 
                    gradual decline in oil production since 2001, the oil and 
                    gas sector has continued its dominance of the economy largely 
                    as a result of the surge in oil prices in the last several 
                    years. Compared to the peak level of oil production of 350mn 
                    barrels in 2000, the level of oil production in 2007 fell 
                    to 259.4mn barrels, representing a 25.9 per cent decline. 
                    The impact of this decline in oil production was more than 
                    offset by a 183.3 per cent increase in average prices of Omani 
                    crude over these seven years.</font></p>
                  <p align="center"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><img src="numbers2.jpg" width="291" height="204" /></font></p>
                  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Crude 
                    oil exports reached 222mn barrels in 2007 compared to 233.4mn 
                    barrels in 2006, registering a decrease of 4.8 per cent. However 
                    the share of oil and gas exports in total exports of Omani 
                    origin decreased from 89.2 per cent in 2006 to 84.8 per cent 
                    in 2007. Its share in total exports (including re-exports) 
                    decreased from 81 per cent in 2006 to 75.9 per cent in 2007.</font></p>
                  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">China 
                    remained the biggest importer of Omani crude oil as its share 
                    of Oman’s total crude oil exports increased from 39.5 per 
                    cent in 2006 to 44.7 per cent in 2007. Thailand came second 
                    with a 17.7 per cent share; Japan was third with a 13.1 per 
                    cent share followed by Korea with a seven per cent share in 
                    2007.</font></p>
