                    </font> <b><font face="Verdana" size="4">‘Net’ Profit</font></b><font face="Verdana" size="1"><br>
                    <b>The ministry of fisheries has devised a range of programmes 
                    and plans to modernise and turnaround this sector. Promoting 
                    traditional fishing remains a priority, though, says HE Dr 
                    Hamed Said Al Oufi, Undersecretary, ministry of fisheries 
                    wealth, in an exclusive chat with Visvas Paul D Karra</b><font color="#990000"><b><br>
                    </b></font><br>
                    </font><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><img src="fisher1.jpg" width="200" height="298" hspace="8" vspace="6" align="left">By 
                    2015, there is going to be a global shortfall of 40 million 
                    tonnes of fish as the appetite for sea food goes up from the 
                    annual 100 million tonnes. In order to feed this voracious 
                    global hunger for fish, an acknowledged wholesome source of 
                    natural protein, fish companies and countries are frenetically 
                    trying to net newer fishing sources.<br>
                    <br>
                    Joining this fishing ‘expedition’ is the Sultanate of Oman 
                    led by the autonomous Ministry of Fisheries Wealth which was 
                    created sometime last year. Creation of a separate fisheries 
                    ministry is seen to be a natural step in the right direction 
                    due to the importance of the fisheries sector in Oman not 
                    the least because of a long coastline (1,700 kms), virtually 
                    unpolluted water and also the economic and social importance 
                    of fishing.<br>
                    <br>
                    Taking the plunge wholesale into the industry has never been 
                    a daunting task because the sea was a primary source of revenue 
                    since aeons ago when much of Oman’s civilisation was consolidated 
                    on the coastline between Dhofar in the south and Al Batinah 
                    in the north.<br>
                    <br>
                    In this modern age, however, the sea no longer holds the money-making 
                    allure. With giant containers ships shuttling to and from 
                    the major ports located at Salalah, Muscat and Sohar signalling 
                    the economic diversification 
                    of the national economy, the idea of fishing as a major occupation 
                    has not caught on in a big way.<br>
                    <br>
				<img src="fisher.jpg" width="300" height="194" hspace="8" vspace="6" align="right">Meanwhile, fat salaries in swanky offices, whether in the 
                    private or public sector, seem to hold more promise than toiling 
                    in fishing trawlers. Therefore the ministry of fisheries has 
                    its task cut out – stem the flow of workforce from the coast 
                    to the city and at the same time make fishing a sustainable 
                    and viable economic activity. Aiding this endeavour is the 
                    Vision 2020, which projects the fishing industry growth at 
                    5.6 per cent per year.<br>
                    <br>
                    HE Dr Hamed Said Al Oufi, Undersecretary, Ministry of Fisheries 
                    Wealth, says that the ministry is constantly evolving itself 
                    to find ways and means to conserve and opitmise the fishing 
                    resources and at the same time make sure that the benefits 
                    percolate to all Omanis by ensuring that enough fish is available 
                    for consumption and by generating revenues for those involved 
                    in fishing activities.<br>
                    <br>
                    Conservation and protection measures include the management 
                    of fish by restricting the commercial fishing seasons for 
                    deep-sea fish and limiting the season for catching species 
                    such as lobsters, abalone and kingfish, which are threatened 
                    with extinction through over-fishing.</font><p align="justify" style="margin: 3px 10px">&nbsp;
                  <p align="center" style="margin: 3px 10px"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"><img src="fisher5.jpg" width="434" height="300"></font>
                  <p align="justify" style="margin: 3px 10px"><font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
                    <br>
                    <img src="fisher2.jpg" width="250" height="152" hspace="8" vspace="6" align="left"><strong>‘Healthy’ 
                    fishing </strong><br>
                    When asked to comment on the fishing industry as a viable 
                    and attractive job option, Dr Oufi laid bare the facts of 
                    this sector. The fishing sector is fully geared to achieve 
                    the objectives of the fishing sector for the Seventh Five-Year 
                    Development Plan 2006-2010.<br>
                    <br>
                    In 2007, there was 151,000 tonnes of landed catch, out of 
                    which 86 per cent was through traditional fishing while the 
                    rest was from commercial fishing including trawlers and long 
                    liners. Around 50 per cent of this landed catch was exported. 
                    The added value of the sector grew by 11.7 per cent to RO85.5mn. 
                    The fisheries exports quantity increased from 46,400 tonnes 
                    in 2000 to 74,000 tonnes in 2007. The value of exported fisheries 
                    rose from RO37.2mn in 2000 to RO59mn in 2007.<br>
                    <br>
                    Dr Oufi emphatically points out the health benefits of eating 
                    fish and says that the whole of Oman’s demand for protein 
                    is sourced from fish. “We have enough and more fish because 
                    50 per cent of Oman’s landed fish catch is exported to 60 
                    countries all over the world. From this we can conclude that 
                    Oman’s supply of protein is secured,” he says.<br>
                    <br>
                    He admits that the food security issue is important and as 
                    a natural renewable resource, fishing has to be carefully 
                    nurtured and the aim should be to develop it as a sustainable 
                    economic activity because, we should be able to give it (fish) 
                    to our grandchildren as we received it from our grandfathers.<br>
                    <br>
                    <img src="fisher4.jpg" width="200" height="260" hspace="8" vspace="6" align="right">Presently, there are nearly 35,000 Omanis employed as fishermen 
                    and if you count an average Omani family of five members then 
                    you have 175000 people who are dependent on fishing for their 
                    livelihood. Another 6000-12000 jobs are created ‘post-harvest’. 
                    This includes the factories where value added manufacturing 
                    of fish products is done, supply of fishing equipment and 
                    other forward and backward linkage processes. This is besides 
                    the hundreds of men involved in logistics like transport of 
                    fish etc.<br>
                    <br>
                    <strong>Corporatising the sector</strong><br>
                    There is still a need to correct the social imbalance and 
                    stop the migration from villages to towns and ensure social 
                    stability in the country. The ministry hopes to change the 
                    behaviour of the youth and incline them to take up fishing 
                    as a long term job opportunity by indirect assistance. In 
                    this regard, the government is encouraging fishermen to modernise 
                    their fleet by buying bigger and modern boats and is also 
                    promoting modern fishing methods by providing the youth with 
                    facilities like fishing assistance tools, winches, gillnets, 
                    haulers, crab haulers, fish finders, GPS etc.<br>
                    <br>
                    Fish boxes for longer storage of fish, better boats to make 
                    fishing safer and also electronic equipment like GPS and fish 
                    finders to find the fish stock easily are some of the other 
                    tools actively promoted among fishermen to sustain themselves. 
                    There are plans to introduce 200-400 boats this year and simultaneously 
                    the government is upgrading the ports to accommodate more 
                    vessels. More money is being pumped with the help of local 
                    and foreign investments to build more factories and improve 
                    the infrastructure for storage of landed fish.<br>
                    <br>
                    As a result of all these efforts, the demand for fishing licences 
                    has been on the rise with a huge waiting list of hundreds 
                    of applications. But the ministry is cautious in granting 
                    licences for fear of flooding the sea with boats and depleting 
                    the stocks of the sea. As such the ministry is granting around 
                    licences to around 20 boats per region per year. The ministry 
                    screens the applications carefully to make sure the applicants 
                    are from the coastal villages. A centralised auction market 
                    is also in the pipeline (see box).<br>
                    <br>
                    <strong>Protecting tradition</strong><br>
                    <img src="fisher3.jpg" width="300" height="232" hspace="8" vspace="6" align="left"> 
                    In order to protect traditional fishermen from commercial 
                    fishing activities, the ministry has demarcated areas for 
                    three types of fishing activities in Oman. The trawler fishing 
                    is allowed 11 miles away from the coastline or 50 mtrs deep 
                    which ever is nearer while the long liners can fish 20 miles 
                    from the coast. Most of the commercial fishing is done in 
                    the areas between south Masirah and Halaniyat islands. Long 
                    liners net the large pelagics like searfish, swordfish, tuna 
                    etc while trawlers get the demersals like hamour, bream etc 
                    and the traditional fishermen are allowed to get all kinds 
                    of fish.<br>
                    <br>
                    Along with the clear demarcations, the government has also 
                    implemented strict monitoring systems to prevent commercial 
                    fishermen from trespassing into the waters where traditional 
                    fishermen net their catch.<br>
                    <br>
                    “We have a 24x7 vessel monitoring system with the help of 
                    a French satellite to monitor all the commercial fishing vessels. 
                    Earlier these commercial fishers used to come at night and 
                    fish in the areas demarcated for traditional fishermen but 
                    this has stopped now,” informed Dr Oufi.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
