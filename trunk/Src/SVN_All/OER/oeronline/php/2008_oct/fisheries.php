				<font face="Verdana, Arial, Helvetica, sans-serif" size="1"> 
                    <br>
                    <strong>Challenges and growth</strong><br>
                    The fishing sector has a number of challenges which are being 
                    addressed by the ministry of fisheries wealth. Some of the 
                    challenges include the sustainable exploitation of fish by 
                    increasing the catch to satisfy the local and international 
                    demand but at the same time managing the resources. One way 
                    to address this issue is to train fishermen in modern technology 
                    to land good quality fish. In order to cope up with these 
                    challenges, the Marine Science and Fisheries Research Centre 
                    has proposed a management plan to prevent overfishing of kingfish, 
                    avalone, lobster etc. The centre is also studying the fish 
                    market, the social aspects of fishing, the biology of the 
                    fish etc. A budget of RO40mn has been allocated in the five 
                    year plan.<br>
                    <br>
                    Fish farming and aquaculture is expected to herald in a sea 
                    change in the fishing sector by increasing the production 
                    of fish as well as creating thousands of jobs for Omanis. 
                    Dr Oufi disclosed that the ministry is in the process of launching 
                    a big development plan for fish farming and aquaculture.<br>
                    <br>
                    With a huge demand gap of 40 million tonnes expected by 2015 
                    globally, the sea cannot provide so much quantity, thus it 
                    will come from aquaculture. Oman can play a major role in 
                    this because it has plenty of land, cheap labour, and a vast 
                    coastline. Presently, the Seabream Farm in Quriyat is exporting 
                    around 500 tonnes to Europe. A 250-hectare Shrimp Farm is 
                    being set up in Mahout to produce 100-200 tonnes initially. 
                    This kind of fish farming will generate thousands of jobs 
                    for Omanis like technicians, administrators and fisheries 
                    science graduates for monitoring of fish and their growth 
                    etc. </font>
