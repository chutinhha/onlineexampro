            <td width="70%" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
                <font face="Verdana" size="4">The Future of Manufacturing<br>
                <font size="1">Mark Eaton, winner of the UK’s prestigious Viscount 
                Nuffield Medal for his contribution to Britain’s manufacturing 
                sector, will deliver the keynote presentation at this year’s PEIE 
                Smart Manufacturing Conference, November 3-4 at the Muscat Inter-Continental 
                Hotel. Eaton talks to OER about defining manufacturing, its future, 
                BRIC economies and In-shoring</font></font></b></p>
              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="4"><font size="1"><strong><img src="face2face.jpg" width="200" height="200" hspace="8" vspace="6" align="left">You 
                believe part of the problem in answering this question is how 
                manufacturing is defined. Perhaps you can start by clarifying 
                it for our readers?</strong><br>
                I agree, the first step to understanding the future of manufacturing 
                is to be clear about what manufacturing really is. A lot of people 
                confuse manufacturing with ‘production’ and associated assembly 
                activities. A couple of years ago a definition of manufacturing 
                was picked up by the UK Government that I believe puts manufacturing 
                in the correct context. The definition reads: <br>
				<br>
				“The modern definition 
                of manufacturing is broad and includes the full cycle of activities 
                from research, design and development, production, logistics, 
                and service provision to end of life management. This definition 
                makes a strong distinction between production (the physical processes 
                of making, bending, etc) and manufacturing (the over-arching process 
                of getting raw materials to goods and their associated services).”<br>
                <br>
                When we put manufacturing in this context, it immediately shows 
                that when we talk about the future of manufacturing we are considering 
                a lot more than just where physical production facilities will 
                be located and the associated production costs.<br>
                <br>
                <strong>This definition of manufacturing makes it appear to be 
                a much bigger problem to define what the future holds for the 
                global manufacturing sector, doesn’t it?</strong><br>
                Well, it does create a problem because of the additional complexity 
                but it also points to some of the issues that will face manufacturing 
                companies in the future, namely that it is not possible to have 
                a completely successful stand-alone manufacturing plant unless 
                you consider the ‘upstream’ (as in supply side) and ‘downstream’ 
                (as in distribution and sales) activities.<br>
                <br>
                With the trend in many countries for manufacturers to specialise 
                and to reduce the capabilities they hold ‘in house’ (as a way 
                of minimising business overheads) it means that many organisations 
                will need to find and work with more partners if they are to be 
                successful. We will see the development of new skills of multiorganisational 
                collaboration and this will mean that the role of the Supply Chain 
                Director will become even more important, but it will also place 
                pressure on organisations to work more effectively with their 
                downstream partners (such as logistics companies and even customers). 
                Some of these skills have been attained already through the process 
                of outsourcing but many companies who have outsourced have found 
                a reduction in flexibility and the need for tighter controls, 
                which flies in the face of the need for a more flexible approach 
                to delivering products to customers.<br>
                <br>
                <strong>What sort of flexibility are you talking of?</strong><br>
                With product life cycles reducing in many sectors, particularly 
                in technologically complex sectors, a new kind of pressure has 
                been placed on companies to develop new products that meet the 
                needs of the market and are delivered both quickly and cost effectively. 
                Indeed, with changing consumer demands in much of the developed 
                and developing world companies need to be flexible about what 
                they offer and how they make it available to their customers.<br>
                <br>
                <strong>So is the future just about collaboration?</strong><br>
                Unfortunately not. Whilst there is a need for companies to be 
                able to collaborate with specialist organisations, something that 
                is facilitated by the technological and information revolution 
                we are in the midst of, the future of manufacturing does not just 
                rely on the ability to collaborate as we cannot ignore the realities 
                of global economics.<br>
                <br>
                <strong>What impact will global economics have on manufacturing?</strong><br>
                To answer that question, I need to discount the current impact 
                of the credit crunch as its long term impact is still being analysed. 
                However, what is obvious is that the (then) low cost, low technology 
                countries who welcomed outsourced production and assembly activities 
                from the developed world in the 1980s and 90s such as China and 
                India have now progressed to being low cost, high/medium technology 
                countries.<br>
				<br>
                To expand on this, the growth in the four countries referred to 
                as the BRIC nations (Brazil, Russia, India and China) has been 
                phenomenal in the last 10 years and has led to the rapid decline 
                of ‘low value add’ manufacturing sectors in many countries where 
                they could not compete on cost, but also, as these countries have 
                invested in innovation, technology and capital building projects 
                they are now competing head-to-head with high value add manufacturing 
                companies and this will be a continuing source of both pressure 
                and opportunities into the foreseeable future.<br>
                <br>
                The impact of this ongoing competition will lead to the need to 
                shorten development times and improve flexibility as previously 
                mentioned but also to start competing more effectively in areas 
                such as brand perception, accessibility (i.e. how easy it is for 
                customers to get access to the product) and other areas traditionally 
                grouped under the heading of ‘differentiation’.<br>
                <br>
                <strong>How will differentiation impact on manufacturers in the 
                future?</strong><br>
                Again, with the technological revolution, successful manufacturing 
                companies will be making use of new technologies and the opportunities 
                it provides for networking and brand development. For example, 
                using www.twitter.com to develop a following from people interested 
                in the organisation’s activities and brand values through to using 
                the Internet to demonstrate the organisation’s ‘Green’ credentials 
                (if this is important to that market’s customers). Allied to the 
                need for differentiation is the careful consideration of whether 
                In-Shoring (the opposite of outsourcing in that organisations 
                source their raw materials locally or even bring production back 
                in house) can provide them with improvements in flexibility and 
                responsiveness that would counteract any possible increases in 
                cost this might incur.<br>
                <br>
                <strong>So, having a clear understanding of who the ultimate customer 
                is and their real needs will be important in the future?</strong><br>
                Being honest, it has always been important and will definitely 
                continue to be so when organisations are visible to millions (if 
                not billions) of people, and in a crowded market it will be necessary 
                for companies to both engage in continuous improvement in their 
                processes and products as well as continuous engagement with their 
                customer base.<br>
                <br>
                <strong>Would continuous engagement with customer not be an expensive 
                cost to manufacturing companies who are better skilled in producing 
                world beating products?</strong><br>
                By continuous engagement I am not proposing the need to constantly 
                spam customers with unwanted information and offers, more the 
                process of being ‘close’ and accessible to your client base which 
                of course requires you to be very clear about who they are, making 
                it easy for them to find you when they need you and making it 
                clear why you are the person to deal with rather than any number 
                of competitors who offer similar products.<br>
                <br>
                <strong>One final question, what role will governments play in 
                supporting their manufacturing base?</strong><br>
                Apart from the need to invest in education so that there are people 
                with the right skills available for companies to employ, there 
                is a need to consider economic policies relevant to their economy, 
                for example, if you want to encourage people to take risks you 
                have to create an environment where they can reduce that risk 
                as far as possible through favourable taxation regimes or even 
                the encouragement of loans at preferential rates.</font></font></p>
