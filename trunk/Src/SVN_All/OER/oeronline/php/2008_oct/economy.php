		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">Mining for the future</font></b><font face="Verdana" size="1"><br>
                    <b>As Oman’s drive to diversify its economy gathers pace, 
                    attention is increasingly turning to building up the industrial 
                    sector. It is natural that this has entailed focusing on utilising 
                    the Sultanate’s largely untapped non-hydrocarbon natural resources</b><br>
                    <br>
                    <font color="#990000"><b>By Oliver Cornock<br>
                    </b></font><br>
                    <img border="0" src="economy.jpg" align="left" hspace="6" vspace="4" width="125" height="148"></font><font face="Verdana" size="1">On 
                    March 20, Mineral Development of Oman (MDO) was launched by 
                    a range of partners, including private sector firms and the 
                    Directorate General of Minerals with the aim of encouraging 
                    and directing investment in industrial-grade mineral mining 
                    in the country. The Directorate General is a government body 
                    charged with ensuring the sustainable development of Oman’s 
                    mineral resources, and its participation is indicative of 
                    the government’s enthusiasm for the project, and should ensure 
                    that it follows the path of the authorities’ diversification 
                    plans.<br>
                    <br>
                    MDO has brought a new dynamism to the mining sector, and has 
                    lost little time in bringing its weight to bear; on June 10, 
                    the group’s first special purpose vehicle, Manar Carbonates 
                    was launched. Manar will produce ground calcium carbonates 
                    and precipitated carbonates from the sultanate’s ample limestone 
                    deposits. It remains unclear what the firm’s target markets 
                    and forecast annual output will be.<br>
                    <br>
                    One of the leading members of the partnership is Al Maha Ceramics, 
                    an Omani manufacturer of wall and floor tiles and part of 
                    Al Anwar Holdings, one of the Sultanate’s largest firms. Al 
                    Maha general manager Suresh Deshmukh sees the project as a 
                    natural step in capitalising on Oman’s high-quality industrial 
                    mineral resources. “There are many opportunities here to develop 
                    non-metallic mineral-based industries,” he told OBG. “The 
                    ceramics industry is based on industrial minerals. As such, 
                    we already have an expertise in processing industrial minerals,” 
                    he added.<br>
                    <br>
                    <strong>Historical precedence </strong><br>
                    Oman’s mining sector has a 4000-year history, as copper was 
                    extracted and smelted in ancient times. The sector’s future 
                    is likely to be based on the exploitation of non-metallic 
                    metals, which are more abundant in the Sultanate and have 
                    a broad range of uses, meaning that they are often less vulnerable 
                    to fluctuations in end-product demand.<br>
                    <br>
                    In addition to ceramics, the Sultanate’s mineral resources 
                    could be used in products including glass, chemical, fertiliser, 
                    agricultural goods, abrasive, fillers, insulation and construction 
                    materials. Oman is strongly placed to benefit from exploiting 
                    its mineral resources at the present time. The managing director 
                    of Al Anwar Ceramics, A Shamsuddin, says that in addition 
                    to the abundant raw materials the sector benefited from the 
                    “relatively low energy costs and proximity to booming construction 
                    markets.”<br>
                    <br>
                    <img src="economy1.jpg" width="186" height="376" hspace="8" vspace="6" align="right"> 
                    Shamsuddin remains convinced that the outlook is very good, 
                    given the strength of the building industry in the region. 
                    “We do not foresee a construction slowdown in the Gulf Cooperation 
                    Council (GCC) markets because high oil prices are driving 
                    government spending in infrastructure projects,” he says. 
                    “Additionally, the young population of the region is in need 
                    of homes, not to mention that the emphasis on tourism and 
                    freehold properties is further fuelling the sector.”<br>
                    <br>
                    To give one example, Shamsuddin asserts that growth of demand 
                    for ceramic tiles, which he estimates at around 15-20 per 
                    cent annually, continues to outstrip increases in output, 
                    which is approximately 5-8 per cent per year. It is because 
                    of this that Al Anwar has significantly boosted the output 
                    of its Nizwa plant, increasing its capacity by 3.5 million 
                    sq mtr to 10 million sq mtr annually. Meanwhile, plans are 
                    already afoot to increase output at Al Maha’s plant at the 
                    Sohar Industrial Area, which only launched operations in April. 
                    “Because the region is a net importer of ceramic tiles, this 
                    presents a clear opportunity for local manufacturers. The 
                    market for ceramic tiles in the GCC is 150m metres square 
                    per annum. The GCC can produce only 50 per cent of what is 
                    needed and therefore imports the rest,” says Shamsuddin.<br>
                    <br>
                    It is then little surprise that MDO has gained so much momentum 
                    in a relatively short space of time. While resource availability, 
                    geography and a strong market coincide, however, there are 
                    challenges that the new outfit must face. While it will benefit 
                    from high commodity prices (which, with demand strong, it 
                    can pass on to its customers), it will also have to contend 
                    with the cost of capital goods and those non-native raw materials 
                    needed for extraction and manufacture, as these will have 
                    to be imported. The recent strengthening of the US dollar, 
                    and therefore the rial, is recent good news on this score, 
                    though the recent shock of the collapse of Lehman Brothers, 
                    the investment bank, and alarm bells ringing over AIG, the 
                    world’s largest insurer, could send the greenback downwards 
                    again, particularly if the US Federal Reserve elects to cut 
                    interest rates. Having said this, rising commodity prices 
                    are also easing the competitive heat that MDO and its partners 
                    feel from China – according to Shamsuddin “the Chinese threat 
                    of cheap imports is diminishing for a variety of reasons, 
                    including higher shipping costs”. Rising wages are another 
                    factor that is eroding China’s price competitiveness, a factor 
                    that will not have escaped notice in Oman.<br>
                    <br>
                    Finally, with the global oil price dipping below $100 in August, 
                    Oman will have slightly less cash to invest in the industry 
                    than might have been the case, given the smaller margins on 
                    its number one export. Still, with global demand still trending 
                    upward, a collapse in oil seems unlikely, and revenues look 
                    set to continue to pour into the national coffers. What is 
                    more, the fall in the price of oil serves to highlight the 
                    importance of investing in diversifying the Sultanate’s economy.</font><p style="margin: 3px 10px">&nbsp;
