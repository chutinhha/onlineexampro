                    </font><b><font face="Verdana" size="4">Hewitt opens registration 
                    for ‘Best Employers in Middle East 2009’ study</font></b>
                  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    Hewitt Associates, a global human resources consulting firm, 
                    and Oman Economic Review have launched the Middle East version 
                    of Hewitt’s global ‘Best Employers’ study. Organisations can 
                    now register to participate in this study. Through this research, 
                    participating organisations will be able to measure their 
                    effectiveness in creating a workplace that both engages the 
                    hearts and minds of employees, and one that leads the market 
                    in their people practices. Interested organisations should 
                    register now on www.hewitt.com/bestemployersmiddleeast. <br>
                    <br>
                    Debabrat Mishra, Middle East Consulting Business Leader, Hewitt 
                    Associates, said: “We are very excited about the launch of 
                    Hewitt’s first ‘Best Employers in Middle East’ study. As the 
                    region is becoming extremely competitive in terms of attracting 
                    and retaining the right talent, companies need to be able 
                    to differentiate themselves. The Hewitt Best Employers study 
                    provides a credible benchmark for employers to create a competitive 
                    advantage through their people.” Given the global success 
                    of this initiative, the Best Employers in Middle East study 
                    is expected to generate a high volume of interest. <br>
                    <br>
                    Hewitt is also contributing to the ongoing efforts of regional 
                    governments to create an economic environment that is conducive 
                    to growth and yet meets the aspirations of individuals seeking 
                    positive work environments. To this end, it has partnered 
                    with United Press and Publishing publishers of Oman Economic 
                    Review to launch this inaugural study. The study is divided 
                    into six phases: registration (now open), data collection, 
                    data collation, random audits, judging, and results. Organisations 
                    that have over 100 full time equivalent employees (FTEs) and 
                    have been in business from September 2007 or earlier in their 
                    country of participation can register to participate in the 
                    study online at www.hewitt.com/bestemployersmiddleeast. Data 
                    will be collected using three proprietary tools developed 
                    by Hewitt: the Employee Opinion Survey (to gauge the level 
                    of employee engagement and gather information on employee 
                    perceptions of their work environment); the People Practices 
                    Inventory™ (to gather information about philosophies, practices, 
                    and policies that influence the management of people); and 
                    the CEO Questionnaire (to explore the alignment between business 
                    strategy and people policies).<br>
                    <br>
                    The Best Employers will be selected by an independent judging 
                    panel including representatives of academia, government organisations, 
                    the human resources profession and the business community. 
                    The judging panel evaluates information on a ‘blind’ basis 
                    whereby the judges are unaware of the identity of participating 
                    organisations until after decisions are made.</font> 
