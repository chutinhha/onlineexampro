                </font><b><font face="Verdana" size="4">Born leader<br>
                </font></b><font face="Verdana" size="1"> <b>Dr Adil Saeed Al 
                Shanfari, Vice Chairman and CEO, Shanfari Group of Companies believes 
                in creating an environment where his co-workers can contribute 
                their best. Mayank Singh reports</b><br>
                <br>
                <img src="person1.jpg" width="250" height="359" hspace="8" align="left">I 
                like to walk from place to place and I am usually too fast for 
                other people,” says Dr Adil Saeed Al-Shanfari, Vice Chairman and 
                CEO, Shanfari Group of Companies explaining his fitness levels. 
                The ‘too fast for others to catch’ adage fits in perfectly with 
                his business acumen. Having joined the Shanfari Group in 1988 
                he has been instrumental in taking the group to new heights. And 
                recognition has been quick to follow – Shanfari has recently been 
                conferred with an honorary doctorate in business management from 
                Woosuk University of Korea. Says Shanfari, “It is a great honour 
                that the best university in Korea wants one to carry its name.”<br>
                <br>
                <strong>Down memory lane</strong><br>
                Shanfari realised early on in his career that the group needed 
                to add new products to its portfolio. Having been exposed to stellar 
                brands like Ferrari, Lamborghini, Jeep and Fiat during his visits 
                to Italy, he was adamant on bringing these brands to Oman. The 
                Shanfari group used to market Jeeps brought from Egypt in the 
                Sultanate. Once the Arab League imposed its boycott on Israel, 
                it became difficult to source Jeeps from Egypt. Not one to give 
                up easily Shanfari kept an eye on an opportunity to get back the 
                brand. When Chrysler purchased American Motors in 1987, it got 
                the Jeep marquee. Shanfari flew to Detroit in the US to pitch 
                in for a dealership for Jeep in Oman. His perseverance paid off 
                as Shanfari Motors succeeded in obtaining the exclusive agency 
                rights for Jeep in the Sultanate. “One has to constantly try to 
                expand ones business. As the external environment changes, one 
                cannot keep everything at the same size,” says Shanfari. While 
                pleased with the economic growth that the country has seen in 
                the last five years, he feels that the rampant increase in prices 
                is adversely affecting a number of people.<br>
                <br>
                <strong>Managerial must haves</strong><br>
                An easy going person Shanfari believes in giving his managers 
                a comfortable working environment so that they can give their 
                best. This freedom though comes with its own share of accountability. 
                As long as a department head works within the confines of their 
                business plan and budget he finds no reason to interfere in anyone’s 
                work. “A person becomes a real leader when others want him to 
                lead them. I do not like people using their power to control other’s, 
                everyone should feel free to express their opinion,” says Shanfari.<br>
                <br>
                <img src="person.jpg" width="200" height="334" hspace="8" align="right">Having finished 
                his schooling from Sultan School in 1983 he moved onto the US 
                to pursue a bachelor’s degree in general business from the University 
                of Denver, Colorado. An outgoing person, Shanfari used the time 
                spent abroad to acquaint himself with other cultures and people. 
                “When I send my children abroad for higher studies I tell them 
                not to just hang around only with Arabic students.” He recollects 
                meeting people who have spent decades in a country like the US 
                or UK without making an effort to either learn the language or 
                get to know the people there.<br>
                <br>
                A workaholic at office, once home it is all about spending quality 
                time with his family. As a principle he does not talk about business 
                at home and neither does he take official calls. Evenings and 
                weekends are spent in the company of family and friends. Outings 
                include taking children out on picnics. He is also not averse 
                to the idea of rustling up a quick meal for his children.<br>
                <br>
                Certain weekends are spent in pursuing his other hobby – fishing. 
                Fishing gives him a high as it helps him indulge in two of his 
                biggest passions – being close to nature and a love for adventure. 
                “I do not like a house that does not have a garden.” A penchant 
                for adventure sees him indulging in a host of outdoor activities 
                like horse riding, martial arts, scuba diving, bungee jumping 
                etc.<br>
                <br>
                Though an indulgent parent, when it comes to education he brooks 
                no compromises, “I want my kids to get a good education and give 
                them a hard time if they are not serious about their studies.” 
                Most of his children have been sent to military schools in the 
                US to gain the discipline and mental strength that comes from 
                such an education. He has made it clear to the next generation 
                that they need to carve out their own space under the sun rather 
                than taking the easy option of joining the family business. “I 
                will not buy them a car or give them money,” says Shanfari in 
                a matter of fact tone.<br>
                <br>
                Having taken a ten-day vacation recently after a number of years 
                Shanfari is effusive about his love for travelling, “I love visiting 
                new places as one gains knowledge by travelling.” He chooses a 
                destination based on the purpose of the visit.<br>
                <br>
                <strong>Leisure time </strong><br>
                A movie buff Shanfari has over the years built up an enviable 
                collection of DVD’s. says Shanfari, “The star cast of a movie 
                does not bother me much as I like a movie with a good story and 
                acting.” Apart from DVD’s he picks up watches and electronic items 
                regularly in Oman and during his travels abroad.<br>
                <br>
                Coming to music he prefers something that is relaxing and quite 
                rather than too loud. He has been reading two books lately, Age 
                of the Warriors by Robert Fix and Tamerland by Justin Malls. Speaking 
                about his influences he says, “I have learnt a thing or two from 
                a number of people and this comes from the fact that I am always 
                on the lookout to gain more knowledge.” An ability to empathise 
                and a humane trait is what differentiates Shanfari as a CEO and 
                as a person. </font><font face="Verdana" size="1"> &nbsp;</font></p>

