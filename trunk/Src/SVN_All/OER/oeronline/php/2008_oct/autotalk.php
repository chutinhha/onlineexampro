				<p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4">
				<br>
				Prestigious Power</font></b><font face="Verdana" size="1"><b><br>
                    Volvo gives us a lesson on how to turn an ‘uncool’ car into 
                    a ‘cool’ one, with the new V8 powered S80. Writes Malcolm 
                    Xavier Crasta<br>
                    </b><br>
                    &nbsp;<b><img src="autocar1.jpg" width="300" height="289" hspace="8" align="left" vspace="6"></b>While most people used to acknowledge Volvos as good high 
                    quality car practically no one would classify it as a ‘cool’ 
                    car. But, Volvo is in the midst of a design revolution to 
                    change this image and, for the most part, they have succeeded. 
                    With the release of the C70 and recently the C30, Volvo is 
                    clearly aiming for the younger crowd. Even the S40 and the 
                    S60 now look more modern and sportier than their last generation 
                    counterparts. They have now taken a similar approach towards 
                    the S80 and by the looks of it, they have done it again.<br>
                    <br>
                    The S80 is, even by today’s standards, a very handsome car. 
                    The S80 now looks more mature, modern and handsome yet still 
                    retains the design cues that distinct it as Volvo. The changes 
                    are subtle yet noticeable. Take for instance the more rounded 
                    headlamps that taper towards the now more distinct wheel arches. 
                    Even though the car is taller than the previous model, the 
                    longer length, the raised bonnet and smaller windows contribute 
                    to a sleeker look that makes it look lower than it actually 
                    is. <br>
                    <br>
                    Open the door and you will be welcomed into a very simple 
                    yet sensible cabin. The large size allows for an abundance 
                    of space, even in the boot. There is ample amount of articulation 
                    for the seat and steering wheel and even the switchgear is 
                    intuitively laid out on Volvo’s very own ‘floating dash’. 
                    The quality of the materials used on the interior are some 
                    of the best you will find. There are almost no hard plastics 
                    to be found anywhere in the interior and even the attention 
                    to detail is evident. But, as well built as it is, the interior 
                    design is just too similar to its lower end models to make 
                    it feel special.<br>
                    <br>
                    Volvo more than makes up for this in nearly every other aspect, 
                    especially features. Auto adjusting bi-xenon headlamps, auto-dimming 
                    mirrors, rain sensing wipers, a nifty pop-up navigation and 
                    many more are available and then there is the safety aspect. 
                    SIPS (Side-impact protection system), WHIPS (Whiplash protection 
                    system), BLIS (blind spot monitoring system), ABS, DSTC (Dynamic 
                    stability traction control), EBL (emergency brake light) and 
                    many more acronyms will keep you safe throughout your journey; 
                    and should these somehow fail to keep you out of harms way, 
                    the superb passenger cage and six airbags should do the trick.<br>
                    <br>
                    We were provided with a V8 version of the car for testing 
                    from Volvo and what a V8 it was! The 4.4 litre power plant 
                    is capable of producing 315hp and a very healthy 440Nm of 
                    torque. Despite the power at cruising speed, the cabin is 
                    kept quite and comfortable with barely any wind or road noise. 
                    The level of grip and the balance through corners is quite 
                    good for a car of this size. The steering is sharp and responsive 
                    and the damping is great but to get the most out of the car 
                    you just have to aware of its weight and not push it over 
                    the top. The reason for this is because it suffers from the 
                    same problem as most other Volvos – lack of steering feel. 
                    But get to know its limits and it can be very satisfying.<br>
                    <br>
                    While the S80 may not have the prestige or the brag factor 
                    of its European rivals, it definitely matches them for performance 
                    and quality. Overall it is a great car with prodigious power, 
                    comfortable ride, high quality interior and enough safety 
                    acronyms to fill up a novel, and all of this for a price of 
                    just RO20,000! Sounds like a good deal – at least, to us.</font></p>
