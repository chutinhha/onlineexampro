		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><strong><font size="4" face="Verdana, Arial, Helvetica, sans-serif">SQU 
                    graduates bag BBIC award<br>
				</font>
                    </strong>&nbsp;<p align="justify" style="margin: 3px 10px">
					<img src="oerevent.jpg" width="300" height="244" vspace="6" align="left" hspace="8"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">What 
                    stole the show at this year’s Big Business Idea Competition 
                    (BBIC) was eco-friendly way of doing business by SQU science 
                    graduates Rayan Al Kalbani and Yaqoob Al Mahrooqi. To give 
                    shape to their business idea, the duo had set up Mazoon Environmental 
                    and Technical Services. The new entity can help clean up oil 
                    spills in a cost-effective and eco-friendly manner. By using 
                    the biotech medium, they hope their new business idea will 
                    thrive in the Sultanate, considering its billion-dollar oil 
                    industry. Having been chosen as winners of this year’s BBCI, 
                    they are planning to move to the rent-free address in KOM, 
                    which comes as a part of the young achievers’ honour. Besides, 
                    they also bagged RO 5,000 worth cash award. &quot;<br>
                    <br>
                    Bader Al Hamdani, who came second in this year’s competition, 
                    had an altogether different story to tell. Bader, also a theatre 
                    personality, is getting ready to produce Oman’s first 3-D 
                    animated cartoon film. His efforts to be first of its kind 
                    in the Sultanate brought him a cash award of RO 5,000. Bader 
                    plans to hit the theatre with his new film by next Ramadhan. 
                    Hussam Al Amri, who based his business idea on robotics and 
                    embedded system, came third. This year saw the three-year-old 
                    business competition, receiving 50 entries. The competition 
                    is organised by The Knowledge Mine (TKM) incubator programme 
                    based at Knowledge Oasis Muscat (KOM) and Ernst and Young. 
                    The annual event has Sanad, Intilaaqah, Knowledge Horizon, 
                    Infocomm, NCR and Oman Economic Review as supporters.<br>
                    <br>
                    Giving away the awards at a function on September 7 at Inter-Continental 
                    Hotel, Mohammed al Maskari, director-general of KOM, said: 
                    “Over the last three years, the quality of entries to the 
                    competition has never failed to impress me. This year continued 
                    this trend and the judges were impressed with all the presentations. 
                    Mazoon Environmental and Technical Services (the winner) showed 
                    a first-rate grasp of the principles of finance, marketing 
                    and intellectual property.”<br>
                    <br>
                    Hilal Al Ahsani, CEO of the Public Establishment for Industrial 
                    Estates (PEIE), who was also present on the occasion, said, 
                    “It is exciting to see the wealth of entrepreneurial talent 
                    in our midst and very gratifying to be able to help turn these 
                    innovative business ideas into a realty. Next year’s competition 
                    promises to be even bigger and better.” Addressing the gathering, 
                    Philip Stanton, country manager, Ernst and Young, said TKM-E&amp;Y 
                    Big Business Idea Competition has been a huge success and 
                    this innovative ideas would not only the help the business 
                    community but also the society at large.<br>
                    <br>
                    Addressing the gathering, Samer Al Nabhani, director of Sanad 
                    Programme, underlined the importance of the competition and 
                    growth of entrepreneurship and innovation in Oman. Thanking 
                    TKM and others for initiating this bright idea, Rayan Al Kalbani, 
                    executive director, of the winning team, said, “We are looking 
                    forward to joining TKM and taking our business idea forward.”
