				<font size="4" face="Verdana, Arial, Helvetica, sans-serif"> 
                <strong>The glass ceiling – still going strong<br>
                </strong></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><strong>Occupying 
                the CEO’s chair remains an elusive goal for most women executives 
                in Oman. The good news is that things are changing fast. An OER 
                report</strong></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                <br>
                <img src="cover1.jpg" width="175" height="267" hspace="8" vspace="6" align="left">Even 
                before The Wall Street Journal coined the term glass ceiling two 
                and a half decades ago, industry watchers and researchers have 
                debated endlessly about the lack of women in the highest ranks 
                of business. The reasons range from the gender biased ones like 
                women choosing less stressful jobs to spend more time with their 
                family to analytical ones like relationships with colleagues and 
                giving back to society being more important for women than salary 
                or promotion concerns. <br>
                <br>
                A look at the list of the Top 20 influential women in Oman brings 
                out a few skews prominently. The list is dominated by women from 
                affluent and influential families. Professional women managers 
                are few. The only silver lining is women seem to be making their 
                presence felt ac ross sectors like the government, corporate and 
                NGO’s. <br>
                <br>
                A recent research carried out by Catalyst (an organisation focused 
                on women in the workplace) shows that women have a better chance 
                of breaking through the glass ceiling if they work in companies 
                which has several female board directors. The study is based on 
                a study of Fortune 500 companies between 2001 and 2006. Oman seems 
                to buck this trend. <br>
                <br>
                Though Oman along with Kuwait has the maximum number of women 
                on boards as a percentage in the Gulf – 2.3 per cent and 2.7 per 
                cent respectively according to a survey called Power Matters: 
                A Survey of GCC Boards by the National Investor and the Hawkamah 
                Institute, this does not translate into more women in the higher 
                echelons of business. <br>
                <br>
                <strong>Delving deep </strong><br>
                <img src="cover2.jpg" width="175" height="247" hspace="8" vspace="6" align="right">Says Jannat Shafaei, Marketing Director, Bin Mirza International, 
                “A lot of women in Oman do not work for a career but look at work 
                as a mere job. This may have something to do with education as 
                women started going in for professional courses a little later 
                than men.” Having studied marketing for her bachelor’s degree 
                and post graduation Shafaei is passionate about marketing. By 
                comparison she finds working women in Oman lacking the same drive. 
                “Women need to go out and push themselves harder, the key is to 
                be professional,” she adds. <br>
                <br>
                Others feel that there are cultural factors holding back women 
                in Oman. “Though women were given the opportunity to get educated, 
                the final call on whether to work or not still rests with the 
                family and not the individual,” says Pauline Berge, Area Director 
                of Sales and Marketing, Oman, InterContinental Hotels Group (IHG).
                The lack of suitable opportunities in the past may have deterred 
                women from taking up jobs in the past. As the economy was dominated 
                by trading, construction and the oil and gas industry there were 
                few openings that would excite women. The growth of sectors like 
                banking, tourism, telecom, hospitality etc has opened up new vistas 
                for women.</font></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px">
				<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
                <img src="cover3.jpg" width="175" height="260" hspace="8" vspace="6" align="left">Though 
                there may be few women occupying the corner left office, the presence 
                of women in Oman’s workforce is a trend that cannot be disputed. 
                “There is a growing acceptability of women as co-workers and more 
                women are striking the right balance between work and their family 
                life,” avers Jeanan S Sultan, Assistant General Manager, Corporate 
                Banking, Bank Sohar. <br>
                <br>
                Women being proficient multi-taskers achieving a work-life balance 
                is a skill that comes naturally to them. Says Ghada Mohammed Al 
                Yousef, Manager, Corporate Communications, HSBC, “It is all about 
                setting ones individual priorities. When I was raising my children 
                there was a pause in my career but the opportunities remained 
                and I could pick up the threads from where I had left.” <br>
				<br>
				<b>A helping hand</b><br>
                Companies on their part can encourage women executives by providing 
                facilities like cr&egrave;ches for their children during office 
                hours, flexible timing, adequate maternity leave etc. A few companies 
                have started working in that direction. Says Shafaei, “Since we 
                are in the restaurants business we jump at good CV’s from women 
                as families are more comfortable when they see women employees. 
                We give them afternoon shifts so that they can spend time with 
                their families in the evening, we work out more lenient terms 
                like not insisting on wearing T-shirts if they are uncomfortable 
                and so on.” <br>
                <br>
                <img src="cover4.jpg" width="185" height="250" hspace="8" vspace="6" align="right">There are organisations which are equal opportunity providers, 
                so while they are open to give everyone their due there are concerns 
                whether women are ready to take on the challenges like long hours, 
                frequent travelling, stress etc that come with the top job. Says 
                Berge, “Women are already making those sacrifices and if it means 
                doing more, then women are surely ready to take on that challenge.” 
                There is a feeling that in a male dominated working environment 
                women need to overprove their mettle to get ahead in life. <br>
                <br>
                Overall women bring in a number of advantages to the work place. 
                “Women bring a different spin to things as they are more logical 
                and systematic,” says Sultan. The attrition rate amongst women 
                employees is also comparatively lower than men, as they are driven 
                by a number of factors like their comfort levels in an organisation, 
                loyalty and not just money. As more women enter the workforce, 
                it is just a matter of time when we see an increasing number of 
                women comfortably ensconced in the corner left office.<br>
