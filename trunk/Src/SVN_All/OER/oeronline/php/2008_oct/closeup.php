		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Bahrain explores new 
                    economic choices</font></b><font face="Verdana" size="1"><br>
                    <font color="#990000"><b><font color="#000000">The Labour 
                    Fund disclosed its own motto through the term (Tamkeen), which 
                    is Arabic for empowering. Tamkeen intends to empower locals 
                    to be the employee of choice in the private sector. The project 
                    focuses on training Bahraini nationals prior to seeking employment 
                    in the private sector</font></b></font></font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><font color="#990000"><b>&nbsp;<br>
                    By Dr Jasim Husain Ali <br>
                    </b></font><br>
                    <img src="close.jpg" width="104" height="128" hspace="8" vspace="6" align="left">Numerous 
                    indications suggest that Bahrain is embarking on a fresh course 
                    of economic choices ultimately designed to make maximum use 
                    of scarce resources. Amongst others, the measures call for 
                    enhancing official investments in international markets while 
                    reducing state holdings in some local firms. Still, another 
                    policy stresses on productivity in addressing the challenge 
                    of creating jobs for locals.<br>
                    <br>
                    In September, Mumtalakat obtained a $500mn facility from a 
                    syndicate of local banks to invest abroad. Mumtalakat is a 
                    holding company encompassing all state assets in entities 
                    outside the hydrocarbons sector. The entity holds government 
                    investments in some 38 firms including full ownership of Gulf 
                    Air and Bahrain International Circuit (BIC). Formula One championship 
                    takes place at BIC. Other notable holdings entail 77 per cent 
                    stake at Aluminum Bahrain (Alba) and 37 per cent ownership 
                    of Bahrain Telecommunications Company (Batelco). Alba is the 
                    largest industrial undertaking in Bahrain.<br>
                    <br>
                    <strong>Diversifying risk</strong><br>
                    To be sure, Mumtalakat is the investment company for Bahrain 
                    or in charge of state wealth fund (SWF) for the country. The 
                    new drive calls for investing abroad along the lines of Abu 
                    Dhabi Investment Authority. Now, some 97 per cent of Mumtalakat’s 
                    $10bn assets are located in the home market. Clearly, it makes 
                    sense to diversify risk by investing abroad. In fact, Mumtalakat 
                    has not ruled out the possibility of raising additional funds 
                    in order to finance its newly founded investment strategy.<br>
                    <br>
                    In addition, Mumtalakat plans to sell off shares in some of 
                    38 firms under its disposal. The planned divestment of Batelco 
                    is a welcomed move, as it would possibly end any state favours 
                    towards Batelco at the expense of Zain of Kuwait, the second 
                    mobile company operating in the country. Telecommunications 
                    Regulating Authority (TRA) is evaluating the possibility of 
                    allowing a third mobile operator in order to stiffen rivalry 
                    in the telecommunications sector. <br>
                    <br>
                    <strong>Business friendly</strong><br>
                    <img src="close1.jpg" width="150" height="311" hspace="8" vspace="6" align="right">Mumtalakat’s 
                    enthusiasm represents a policy shift within Economic Development 
                    Board (EDB). EDB controls Mumtalakat. Since the start of 2008, 
                    the EDB has assumed sole responsibility for developing and 
                    implementing economic policies. Set up in 2005, the decree 
                    required EDB to coordinate its policies with the prime minister.<br>
                    <br>
                    However, the process of coordination sounded easy on paper 
                    but not in implementation, eventually forcing a separation 
                    of economic and non-economic matters. The cabinet is in charge 
                    of all policies save economic matters. Undoubtedly, this phenomenon 
                    is not a common practice in fellow Gulf Cooperation Council 
                    (GCC) states.<br>
                    <br>
                    Recently, the EDB adopted the motto of “Business Friendly 
                    Bahrain.” The EDB officials hired a consulting firm to come 
                    with the slogan that suggests that Bahrain is the best place 
                    for doing business in the region. Bahrain faces daunting challenges 
                    from other regional players notably Dubai for the title. Yet, 
                    at the least, a key study designates Bahrain as the freest 
                    regional economy. The Heritage Foundation and the Wall Street 
                    Journal consider Bahrain as the freest Arab economy by virtue 
                    of clinching 20th spot on the 2008 Index of Economic Freedom.<br>
                    <br>
                    <strong>Stressing productivity </strong><br>
                    The other notable economic policy emphasis relates to addressing 
                    the job challenge. In an interview with CNN, crown prince 
                    Salman Bin Hamad Al Khalifa stressed on productivity in tackling 
                    employment of locals. According to a report published by the 
                    Financial Times, jobless rate amongst nationals ranges between 
                    eight and 12 per cent. In contrast, the authorities claim 
                    that unemployment rate is below four per cent.<br>
                    <br>
                    At any rate, the jobless debacle becomes more serious by taking 
                    into account matters such as disguised unemployment and underemployment. 
                    Some locals accept jobs not commensurate with their educational 
                    qualifications. A local with a BSc degree in engineering has 
                    reportedly accepted a job at a shop selling sweets. Yet, there 
                    is the case of underemployment, as some nationals work less 
                    than eight hours per day. This is particularly true of women 
                    who work in daycare centres or supermarkets.<br>
                    <br>
                    <strong>Employee of choice</strong><br>
                    The Labour Fund has the mandate to develop and implement programmes 
                    to make Bahraini nationals the employee of choice in the private 
                    sector. The Fund includes representatives from the government, 
                    private sector and the labour movement. The entity develops 
                    schemes fit for local market conditions.<br>
                    <br>
                    For example, the Labour Fund has a scheme designed to train 
                    700 accountants to enter the job market. Demand for accountants 
                    keeps rising with growing sophistication of different economic 
                    activities. Another governmental entity known as LMRA or the 
                    Labour Market Regulating Authority provides the necessary 
                    financing for the Labour Fund.<br>
                    <br>
                    The LMRA makes policies for the job market. Amongst others, 
                    the LMRA sets the amount of fees levied on foreign workers. 
                    Starting from July, the LMRA increased fees of foreign employees 
                    from $533 to $1,164 for a two-year permit. The initiative 
                    aims at encouraging local businesses to consider employing 
                    locals. As of 2007, foreign employees constituted 80 per cent 
                    of total employees working the private sector. Still, the 
                    private sector created 98 per cent of new job opportunities 
                    in 2007.<br>
                    <br>
                    Recently, the Labour Fund disclosed its own motto through 
                    the term (Tamkeen), which is Arabic for empowering. As the 
                    name implies, Tamkeen intends to empower locals to be the 
                    employee of choice in the private sector. The project focuses 
                    on training Bahraini nationals prior to seeking employment 
                    in private sector establishments. Officials are right in rehabilitating 
                    locals prior to entering the job market.<br>
                    <br>
                    It was widely agreed that locals must adopt a culture that 
                    encourages being appreciative and helpful to clients. The 
                    notion of culture is vital, as employers contend that they 
                    could only extend jobs to individuals willing to sacrifice 
                    efforts and time while serving customers.<br>
                    <br>
                    Ostensibly, Bahrain is promoting a new economic course that 
                    intends to make the country as business friendly as possible. 
                    Undoubtedly, regional rivalry is forcing Bahrain to develop 
                    well-thought solutions to economic problems. I had once remarked 
                    that Bahrain faces competition from three places whose names 
                    start with the letter D, namely Dubai, Doha and Dammam. Competition 
                    has since then become global with no frontiers.<br>
