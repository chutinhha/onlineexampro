                    </font><b><font face="Verdana" size="4">The Subprime Impact</font></b><br>
                    <b><font face="Verdana" size="1">The MSM30 falls precipitously 
                    on global cues and a sharp correction in regional and emerging 
                    markets</font></b><br>
                    <br>
                    <font face="Verdana" size="1">MSM30 index took a nosedive 
                    for the period from August 16 , 2008 to September 15, 2008 
                    wiping out billions of rials in market capitalisation. MSM 
                    index continued to head south losing all the gains in 2008 
                    and down 20.6 per cent in the same period to close below December 
                    31, 2007 closing.<br>
                    <br>
                    Global Liquidity crisis and waning risk appetite of foreign 
                    investors were the primary reasons for the sharp correction 
                    in the regional and emerging markets. Average daily turnover 
                    in the same period decreased by 13 per cent (month over month) 
                    to RO 9.6mn a significant decline compared to average daily 
                    turnover of approximately RO20mn in the first half of this 
                    year. The only sectoral index in black was the services sector 
                    while the other two sectoral bourses turned red, on a year 
                    to date basis. The fall can be attributed to foreigner institutional 
                    investors selling in the market. Foreign ownership decreased 
                    from 30.2 per cent in March, 2008 to 24.4 per cent in August 
                    2008. Though the share of foreign investors in the total market 
                    capitalisation seems small, adjusted for free float holdings, 
                    the stake would go up noticeably.</font><p align="center" style="margin: 3px 10px"><font face="Verdana" size="1"> 
