                    plans in Port Sohar with a capacity of 300mt per day at an 
                    estimated cost of RO 14.3mn. <br>
                    <br>
                    <strong>GCC markets turn bearish</strong><br>
                    The GCC markets turned in bearish for the month ended September 
                    15, 2008 and a blood-bath was seen across the GCC bourses. 
                    The overall sentiments in the GCC markets were weak due to 
                    foreigners selling and dropping oil prices. <br>
                    <br>
                    The Doha Security Market was the worst performer and the DSM 
                    20 index plummeted 25.43 per cent pushing it on the number 
                    four position in terms of YTD performance. The industrial 
                    and the insurance indices were the top losers and declined 
                    33.16 per cent and 24.82 per cent respectively. <br>
                    <br>
                    Top executives in the UAE market have come under the scanner, 
                    this coupled with expectations of a correction in the real 
                    estate market impacted the stock market. The DFM and the ADSM 
                    dwindled by 19.40 per cent and 17.20 per cent respectively. 
                    While the real estate index was the common worst performer 
                    on both the exchanges, the banking counters were the other 
                    stocks that got a beating on the DFM and the industrial counters 
                    took up the space in the ADSM.<br>
                    <br>
                    The KSE took a hit of 15.43 per cent and all the major indices 
                    ended in the red. Due to the falling markets the investment 
                    companies took the worst hit and the investment index lost 
                    22.30 per cent from its month ago levels. <br>
                    <br>
                    The Tadawul All Share Index decreased by 11.40 per cent during 
                    the month. The investment and insurance indices declined 21.06 
                    per cent and 15.87 per cent respectively. The Bahrain stock 
                    market was the least affected by the GCC meltdown and the 
                    general index declined 9.40 per cent. The investment and the 
                    banking indices were among the worst performers losing 12.18 
                    per cent and 7.63 per cent
                    respectively. <br>
