                    <p><font face="Verdana" size="4" color="#FF0000">The cable
                    guy</font><font face="Verdana" size="1">
                    <br>
                    <br>
                    <b>Hussain Bin Salman Al-Lawati, the boy who once sold
                    chocolates on the streets of Muttrah is today the managing
                    director of Oman Cables. Vicky Kapur brings to light the
                    electrifying shades of his personality</b></font></p>
                    <p><font face="Verdana" size="1">
                    <img border="0" src="profile.jpg" align="left" hspace="5" vspace="5" width="150" height="151">For us lesser mortals, handling a solitary job becomes more than a handful at times. Not so for Hussain Bin Salman Al-Lawati, who juggles five
                    (that's right) top posts in as many organisations day in and day out. Add to that the fact that he never studied further than higher secondary, and you just can't help but appreciate his mettle.<br>
                    Born in 1955 in a humble and respectable family in Muttrah, Lawati got his first taste of life at the age of five, when financial hardship forced him to sell chocolates on the street.
                    "I had to do it because, being born in a poor family, I had to support my family in whatever way I could," he states as a matter of fact.<br>
                    Lawati attended a private school in the beginning, and then, at the age of seven, had an opportunity to register with the Saidia School, the only government school at that time.
                    "That added a whole new perspective to my life," he maintains. While in school, Lawati focused on English, Arabic, mathematics, poetry and history, but he continued to sell chocolates and even bind books in the afternoons until he started attending the market with his father, who was a traditional broker.<br>
                    &quot;Exposure to the market at that early stage in my life gave me the opportunity to learn from the experienced people there, and those values and wisdom help me even today," affirms Lawati. For the record, the boy who used to sell chocolates on the street is the vice-chairman and managing director of an SAOG (Oman Cables Industry) and on the board of the prestigious Public Establishment for Industrial Enterprises (PEIE), as also the managing partner of three LLC companies (Al-Saleh Enterprises, City Lights Trading [Sharjah] and Jenfel).&nbsp;</font></p>
                    <p><font face="Verdana" size="1"><b> Dare to dream</b><br>
                    That journey from the sandy streets of Muttrah to his classy office in the heart of the business district in the capital was as much fraught with adversity as it was marked by Lawati's resolve to beat all odds and emerge a winner. After finishing his primary education, Lawati joined a night school since he could not afford to not work in the day.
                    "I started working for a bookshop, where I got an opportunity to educate myself by reading different books and magazines both in English and Arabic available there," he reminisces.&nbsp;<br>
 Even before he had finished his secondary education, he began working for a banking institution.
                    "Working for a small branch, I was fortunate enough to gain exposure to a lot of banking activities," he says. From there, Lawati moved on to Ministry of Information for a year. His second quick move came in the form of Sanoco, an American oil company, where Lawati claims to have gained
                    "tremendous experience".&nbsp;<br>
 It was around this time, in 1974, that his maternal uncle, Mustafa Bin Mukhtar, floated a construction activity company by the name of Al-Saleh Enterprises, which was to become a multi-million-riyal business empire.
                    "At that time, I used to attend to Al-Saleh in the morning, my school in early evening, and then work night-shift at Sanoco," says
                    Lawati. "That's how tough life was," he adds.</font></p>
                    <p><font face="Verdana" size="1"><b>Sweet success</b><br>
                    It wasn't until 1979 that Al-Saleh started doing some serious business, and employed professional bookkeepers.
                    "We struggled hard to establish Al-Saleh and, initially, our daily share did not even amount up to RO2," says Lawati with a wry smile.
                    "I remember that our first achievement after a few years was our first professionally accounted turnover of about RO16,000," says Lawati with a gleam in his
                    eyes.<br>
                    There was no looking back since then. Starting off as a small construction activity company and then venturing into electrical trading, today, Al-Saleh specialises in the illumination business - indoors, outdoors, as well as industrial lighting and lighting appliances.
                    "Besides these, our main focus now is on luxury products that are part of the modern-day housing and office equipment," acknowledges Lawati.&nbsp;</font></p>
                    <p><font face="Verdana" size="1"><b> The big bang</b><br>
                    "Oman Cables Industry (OCI) was born as an idea in my mind in 1978, and this thought kept developing and maturing in my bones and skin until after two years, when I proposed it to our chairman Mustafa Bin Mukhtar, who welcomed it immediately," recalls
                    Lawati. OCI's raison d'etre, according to Lawati, was that he wanted to
                    "contribute to national growth besides, of course, making oneself grow along with society."<br>
                    After his uncle accepted the idea - not before thorough review and evaluation - Lawati began studying in real earnest more than 17 plants worldwide.
                    "I visited those plants at my own cost to form an opinion about the working of the cable industry and to figure out the way ahead," avers
                    Lawati.<br>
                    At that time, the company was named Electrical Wire &amp; Cable Manufacturing Company. Within a year, the company was renamed Oman Cables Industry, which was thought to be
                    "a more appropriate name that could reflect the activity better and help build the image of the company in the long term."&nbsp;<br>
 Beginning with a humble workforce of 10 employees - including the management - OCI has today come a long way indeed.
                    "I am extremely pleased to note the efforts of our team that have borne fruit and today, OCI is a symbol of success, quality not only across the country, but across the region," says Lawati.</font></p>
                    <p><font face="Verdana" size="1"><b>Geared for growth</b><br>
                    "We now want to maintain this reputation and image of this successful company, which isn't an easy task, but considering the ups and downs that we have witnessed, we will continue to ensure further growth of the company and to be on the safe track," pronounces Lawati.&nbsp;<br>
 But how does he intend to keep the company on the safe track in this part of the world, where, unlike in the Western world or in diversified economies, the resources are limited, and in case there is a problem with any of the resources, the economy as a whole gets affected?
                    "Financial fitness is not always the correct yardstick for success. Sometimes, profitability has to be sacrificed at the altar of real success," he answers.<br>
                    "Real success," according to Lawati, "lies in continuous growth as opposed to fast growth which the numbers may reflect," says
                    Lawati. "The long wisdom has got its own cost, and not everyday is Friday - there are bound to be ups and downs in a business. What matters is the commitment," he
                    adds.<br>
                    OCI posted a net profit of over RO1.09 million on an income of RO20.8 million in 2002, a 25 per cent growth over 2001. The company also announced a cash dividend of 17.5 per cent per share in 2002. (For details, refer to Oman Economic Review, July 2003
                    issue.)</font></p>
                    <p><font face="Verdana" size="1"><b>Personal prism</b><br>
                    Married in 1982, Lawati is the proud father of three daughters and two sons. Two of his daughters are studying in Australia - one completing her business management in Brisbane and the other studying genetics. His third offspring, Salman, who's cleared his higher secondary examinations with flying colours, is interested in pursuing a career in Electro-Mechanical Engineering. Salman's younger brother, Hamza, is heading towards first secondary, and the youngest of all siblings, Zahira, is in sixth
                    standard.<br>
                    Lawati's hobbies include reading and watching Arabic and international news channels - anything that helps him grow as a professional.
                    "I played hockey in my childhood, but discontinued after I got hit on the hip and realised that I could not afford to be away from work," laments
                    Lawati.<br>
                    A compulsive morning and evening walker, Lawati is ready for work by 7 a.m. in summers and by 8 a.m. in winters. A self-confessed workaholic, Lawati says that he is now trying to improve (on doctor's advice). In fact, there was a time when, in 1986, he had to travel to Milano, Italy for an assignment and had to work for 36 hours at a trot without a wink of sleep.
                    "I rested only after I collapsed, but not before I finished the job I was entrusted upon," gleams
                    Lawati.&nbsp;</font></p>
