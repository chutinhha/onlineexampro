					<p align="justify" class="style13">In a country like Oman where Building Society culture has not evolved as such, the reputation and experience of the developer is the only thing one can go by
 
<br />
<br />
Right from the caveman, mankind has had an innate urge to own a home. It is a source of security, a source of pride and status and it satisfies a basic human instinct and necessity. Everyone aspires to own his or her dream home.
There are a few things to take care of while acquiring a house. In almost all cases, one’s home is the largest asset one possesses and it is more of an investment than an expense. Therefore, one must invest wisely. 
<br />
<br />
Since, a home is an investment, one should look for appreciation. There are some fast growing areas and there are some declining ones. One must make sure that one is buying in a fast growing location. Even within a given location, one can find that there are areas which are preferred and others not. Choosing a good location is one of the most important things. Besides appreciation, one should look for a location close to work and schools. 
<br />
<br />
It was common in Oman to buy a piece of land and get a contractor to build a house. Although this is still possible, one now has many options from property developers especially in major cities. The advantage is that developers build a “neighbourhood” and also know how to better build quality. It is common to have offerings with swimming pools, landscape area, gymnasium, 24-hour security etc. Of course, if one is buying a new apartment rather than a villa, one has to necessarily buy from a developer. 
<br />
<br />
One should shop around and get to know prices. There are websites in Oman which list properties from major real estate brokers and they are a rich source of information. It is also advisable to get a valuation done by a reputed valuation firm so that one is fairly sure about the correct price. 
<br />
<br />
Usually, if one is buying a new home from a developer, it is often off plan. One must be extremely careful when buying off plan, because one is handing over money with little security other than a one sided contract drafted by the developer. There are still no stringent laws in Oman governing the relationship between property developers and buyers and one is better off depending on the reputation of a developer rather than what is written in to a contract. One must look closely at the track record of the developer in delivering promised quality and finishes in the contracted time frame. 
<br />
<br />
Where one is buying a new apartment or a villa in a complex, one must closely look at the facilities management arrangement over a long period. “Building Society” culture has not evolved as such in Oman and one is dependent on the arrangements promised by the developer. The reputation and experience of the developer is the only thing one can go by. 
<br />
<br />
One could buy in the secondary market where the advantage is that you get what you see, although the property would have depreciated according to its age. It is advisable to get an engineering consultant to certify the quality. 
<br />
<br />
In almost all cases, property is bought with a mortgage. Almost all banks offer home loans and one must shop around for the best deals. One should never hesitate to borrow because real interest rates (nominal adjusted for home price inflation) are usually very low and are frequently negative. 

