					<p align="justify" class="style13">Aviation industry is one of the biggest facilitators of tourism growth in the Middle East region. With its massive investment on aviation infrastructure, the Sultanate is keen on developing the sector as an enabler of tourism development and diversified economic growth. <br />A report by Muhammed Nafie
 
<br />
<br />
The “If you build it, they will come” axiom holds true for the tourism development in the GCC region. Unlike established global destinations known for the attractions they offer, tourism in the GCC region is not distinct from the infrastructure that makes it possible. Some of the most important factors that triggered the recent surge in the number of people visiting the region include massive investment on aviation infrastructure coupled with the rise of major air transport hubs as well as the market entry of low cost carriers in recognition of the untapped demand for intra-regional air travel. There is no denying the fact that aviation is one of its biggest facilitators of tourism in the region as the growth in this sector is intimately bound up with national economic strategies and airport development; and most of the countries in the region have long recognised aviation’s strategic importance and its potential for creating growth in other sectors.
<br />
<br />
According to the United Nations World Trade Organisation (UNWTO)’s global report on aviation, the international arrivals to the Middle East will almost double from 36 million in 2010 to 69 million by 2020. Corroborating these predictions is the Global Market Forecast 2013 by Airbus which states that the Middle East region has led the world’s air traffic and tourism growth in 2012. Unveiling the report recently at the third Arab Aviation & Media Summit in Salalah, Andrew Gordon, director of strategic marketing and analysis market forecast at Airbus, said the region is outpacing world economic growth and Oman is expected to outpace Middle East growth. “In 2010, the region saw 77.1 million passengers being carried by airlines generating $49.5bn in revenue. In addition, air transport supports 2.7 million jobs and accounts for $129bn of the GDP of the region. Traffic from and to the Middle East has grown by 236 per cent over the last 10 years.”
<br />
<br />
The report predicts that the Middle East aviation market will witness significant growth over the next 20 years with passenger traffic forecast to grow 6.2 per cent annually; and the global share of regional carriers will grow from a seven per cent to 11 per cent in the next 20 years.
<br />
<br />
Airlines from the region are expected to receive delivery of 1,963 new passenger and cargo aircraft. As a hub for both leisure and business travel, the Middle East is in a particularly advantageous position to benefit from this growth, with emerging economies such as Oman likely to see significant expansion of their aviation sectors in the coming years.
<br />
<br />
Gordon added that low cost carriers (LCC) in the Middle East have also grown in traffic and market share. The Middle East fleet is expected to grow more than 2.5 times. The fleet in the Middle East which stood at 828 aircraft in the beginning of 2012 will reach 2,212 by 2031. By 2031 a total of 1,963 new aircraft are expected to be delivered, with a market value touching $408bn.
<br />
<br />
Adel Ali, CEO of Air Arabia, the organisers of the summit, said although aviation and tourism sectors have immense potential for growth in the Arab world, there are a number of significant challenges that still need to be addressed if it is to be fully realised. The starting point is the implementation of a full open skies policy, the introduction of more liberalised regulations allowing the free-flow of cross-border movements, and the proliferation of privately owned airlines. Together, this approach would help the Middle East aviation and tourism sector to reach its full potential, which is estimated to be equal to the size of the US economy.”

<br />
<br />
<strong>
Massive investment in Oman </strong>
<br />
<br />
Oman’s focus on developing tourism infrastructure including its massive investment in aviation sector is one strand of an economic development strategy to diversify the economy away from the hydrocarbon sector. The strategy to revamp air transport infrastructure in the country includes a massive expansion of two international airports, building three Greenfield regional airports and strengthening the national carrier Oman Air. The Salalah airport expansion will be completed by the end of 2013 and commissioning is expected by early 2014, while Muscat airport expansion will be completed six to nine months after commissioning Salalah’s airport expansion. 
<br />
<br />
HE Maitha Al Mahrouqi, undersecretary at the Ministry of Tourism, who attended one of the panel discussions at the summit, said that the ongoing development and expansion of airports in the Sultanate would play a big role in the growth of the industry. “The aviation sector is very important for the growth of the tourism industry and it is very important that the tourism and aviation sectors work in tandem,” she explained. “The number of hotel rooms is growing with the growth of the aviation sector. The source market for Oman tourism has been regional countries, but Europe is a big market and Asia is also booming. We are looking to develop these source markets.”
<br />
<br />
HE Maitha revealed that tourism sector’s contribution to Oman’s GDP was at 2.4 per cent and it would grow to three per cent as the Sultanate works to meet projections set as part of Vision 2020. “We have made massive investments in the industry and are working on different initiatives, of which ITC projects and the Oman Convention and Exhibition Center (OCEC) are the major ones. The number of tourists visiting Oman has touched 1.4 million in 2012 and various steps are on to increase the numbers. The government has signed memoranda of understanding with several countries to further upgrade tourism sector which was not affected by the turmoil in the Middle East region during the last two years,” she added.
<br />
<br />
Giving an overview of the new airports being built in Oman, Salim Nasser Said Al Aufi, CEO of the Public Authority for Civil Aviation, who was the guest of honour at the summit, said that the overall investment for Muscat, Salalah and regional airports was estimated at RO3bn. “The authority is working on the phase three contract for the three regional airports in Sohar, Duqm and Ras Al Hadd, on the terminal building, control towers, and support buildings,” he said. He also added that the Tender Board was studying them and they would be tendered in the next few months. He also pointed out that the sultanate would have at least one LCC by the first half of 2014 with the possibility of more licences being awarded. 
<br />
<br />
“Since low-cost carriers currently enjoy a seven to ten per cent share of the Oman market, we want to introduce a local low-cost carrier that can serve the local market,” he said. “The Authority is looking into all civil aviation services that are not currently available in Oman, including budget airlines, executive aviation and helicopter aviation.” He added that the aviation sector in Oman was to undergo a major restructuring within the next two to three years. The introduction of budget airlines will definitely increase the market share of LCC in Oman to 10-15 per cent.
<br />
<br />
However, in order to fully utilise the massive facilities coming up, the government needs to introduce an ‘open sky’ policy which is essential to lure more low-cost and legacy carriers, thereby helping airports generate good return on investments.
