					<b><font face="Verdana" size="4" color="#C00000">Sharpening the edge</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Although they finished ninth out of 15, for Oman’s national golf team the 33rd Pan Arab Golf Championship held at Dreamland Golf & Tennis Resort in Cairo was an opportunity to hone their golfing skills ahead of bigger challenges to come</font></b>
                        <br />
                        <br />
						<center><img src="images/golf_update.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">In what was an exciting opportunity to brace themselves for upcoming regional challenges ahead, Oman National Golf Team participated at the 33rd Pan Arab Golf Championship, held at Dreamland Golf & Tennis Resort in Cairo, from June 25 to 28, 2013. As many as 15 countries participated in the tournament, which is the second largest regional amateur event for Arab golfers after the Mena Tour.
The Omani golfing team who played in the tournament comprised Ali Hameed Al Saleh, Hamed Al-Rumhy, Ahmed Mohammed Al Balushi, the youngest of the lot who made his début in the men’s division after an outstanding performance at the 17th GCC golf event this year, and Oman’s No 1 golfer Azaan Al Rumhy, who has been enjoying a creditable performance due to his consecutive winning streak in the last few major golf competitions held in Oman. 

<br />
<br />
 Accompanying the team was Marcus Casey, team coach and PGA professional from Al Mouj Golf; heading the delegation was Mundhir Al Barwani, team captain and chairman of OGC together with Ahmed Al Jahdhamy, OGC’s vice chairman from the Ministry of Sports and Youth Affairs. 
 
<br />
<br />
 The Almouj Golf Swiss Air Par 3 League Series 1 Final followed, with ten players making the cut from the previous six weeks of qualifying. The golfers competed for a pair of Business Class flights to Dubai, courtesy of event sponsors Swiss Air. Nasaruddin Mohammad took top honours with a fine performance of nine straight par 3s for a net total of 24, while Johan Keyter and Robert Daniel shared a net score of 26, only to have Daniel come in second after a countback over the last six holes. 
 
<br />
<br />
Azaan Al Rumhy said he had very mixed feelings about the tournament. “I am pleased that I tried my best to shoot in the Low 70s, except on the last day when I shot a 78 as the wind blew stronger and the course played harder due to tight pin positions on most of the holes. I finished tied 17th individually with rounds of 76, 75, 76 & 78,” he said. “Obviously disappointed about finishing ninth as a team out of 15 countries, we were hoping to finish top six at least but the other countries were much stronger this year compared to previous years. I was very impressed with Ali Hameed’s performance during the week as he also managed to score all four rounds in the 70s with rounds of 78, 77, 77 and 79 finishing 27th individually. It was unfortunate that we couldn’t get a strong third score in all four days which had affected our team position as the format counts the best three out of four scores each day. Ahmed didn’t get enough time to practice after his final year exams which ended only three days before the event and playing for the first time with the main national team. And Hamed had injury on his lower back that affected his swing and game during the tournament. I am sure the team will get stronger the more we practice and take part in these big golfing events.”
<br />
<br />
Mundhir Al Barwani, chairman of Oman Golf Committee, said, “I am confident that together with experiences gained from participating at the 33rd Pan Arab Championships along with other regional and international golf tournaments, our national team will improve. Golf in Oman is still in the developing stages. We have had only green golf courses during the last three years. Funding OGC is crucial for us to provide the best training and developing programmes for our national team, which is key to making sure they do better at these regional events.” 


