					<b><font face="Verdana" size="4" color="#C00000">On the fast track</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Bank Sohar has been named as the ‘Fastest Growing Bank in Oman’ at the Banker Middle East Product Awards 2013 instituted by CPI Financial</font></b>
						
						<br /><br />
						<center>
						<img src="images/in_the_news_2.jpg" border="0">
						</center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
Demonstrating excellence and the fastest rate of growth amongst its competition in the banking sector in the Sultanate, Bank Sohar scooped the prestigious ‘Fastest Growing Bank in Oman’ award for the second year running, establishing the bank’s increasing presence in Oman.
<br />
<br />
The prestigious accolade was conferred on Bank Sohar at a ceremony held in Dubai recently at the Emirates Towers Hotel. Dr Mohamed Abdulaziz Kalmoor, CEO of Bank Sohar accepted the award on behalf of the bank, while Abdulali Abdullah Al Lawati, senior AGM – corporate support, and Mazin Mahmood Al Raisi, AGM & head marketing and publicity, were also present at the ceremony. CEOs and top-flight bankers representing leading institutions from across the Middle East and Africa region also attended the gala event.
<br />
<br />
 “Receiving this award for the second year is a singular honour for Bank Sohar and is a testament to its commitment to excellence, which is the bank’s core philosophy”, commented Dr Mohamed Abdulaziz Kalmoor, CEO of Bank Sohar.
He also added, “It gives us great pride to be recognised for our growth, only six years from commencement of our operations. In a heavily competitive market, to receive recognition such as this is indeed encouraging for our dedicated employees who drive our customer-centric banking model, as well as a measure of success for our customers and other stakeholders, who have put their faith in us.”


<br />
<br />
Dr Kalmoor added, “Bank Sohar’s assets have also grown remarkably over the past six years. At the close of business in 2012, customer deposits grew by 14.17 per cent to reach RO1,337mn, from RO1,171mn in 2011. Net loans and advances jumped by 12.4 per cent during the year to touch RO1,146mn, against the previous year’s level of RO1,020mn. Net profit at the end of 2012 jumped an impressive 58.73 per cent to RO23.011mn, compared to the previous year’s earnings of RO14.497mn. Operating income rose by 21.14 per cent to RO52.317mn in 2012, from RO43.186mn in 2011.”
<br />
<br />
A place among the top performing banks within the Middle East region is every major banking institution’s ambition – a recognition that came Bank Sohar’s way in a mere six years since it commenced operations in 2007.
<br />
<br />
The Banker Middle East Product Awards 2013 are conducted by CPI Financial, one of the region’s most influential publishers of business and financial news through digital and print media. The awards are amongst the most sought-after honours in the banking industry. Significantly, the award represents another ‘feather in the cap’ for Bank Sohar as it celebrates six successful years of commercial operations in the Sultanate. Over the past six years, Bank Sohar has made impressive strides in its growth. Launching operations as a one branch outfit in April 2007, the Bank’s network has burgeoned into 25 branches across the Sultanate as of June 2013. 
<br />
<br />
In fact, the bank also won the distinguished Tatweej Academy’s ‘Golden Order of Merit in the field of prudent management and the best supportive bank for tourism projects in the Arab world at a ceremony held on June 15, 2013. Winning these two prestigious awards back-to-back stands as a testament to the bank’s commitment towards banking excellence and its steadfast dedication towards the wellbeing of the community. 
