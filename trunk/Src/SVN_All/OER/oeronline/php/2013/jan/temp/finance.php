						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>25 years of enabling 
						dreams</strong></span><p class="style15">
				<span class="style14"><strong>Muscat Finance (SAOG), the pioneer 
				of non-banking financial services in the country, is celebrating 
				its 25th anniversary this year. Visvas Paul D Karra traces the 
				history and performance of this company in this special report</strong></span><p class="style15" style="text-align: center">
				<span class="style14"><strong><br>
				</strong></span>
				<font face="Verdana" size="1">
				<img border="0" src="images/finance-oct-12-1.jpg" style="border: 1px solid #808080; " hspace="10" vspace="4"></font><p align="justify" class="style13">
				Muscat 
				Finance (MF) is the pioneer of non-banking financial services 
				sector in the Sultanate having established itself in October 
				1987, under the leadership of HE Dr. Omar Zawawi. With a small 
				capital base, MF made a humble beginning but today boasts of 
				having constantly created value for its shareholders by 
				excellent performance and high profit growth every year (except 
				1990, the year of the Gulf crisis). MF has an unmatched record 
				of dividend payout right from its inception enriching its 
				shareholders by providing significant returns.<br>
				<br>
				As it celebrates its silver jubilee this year, MF officially 
				unveiled a significant change to its corporate identity. The new 
				logo embodies the dynamic spirit of the company that has 
				pioneered the concept of hire purchase, equipment leasing and 
				debt factoring in the Sultanate. A new tag line – ‘Enabling 
				Dreams’ – eloquently summarises the continued promise of being 
				an experienced and professional financial facilitator, 
				fulfilling the dreams and aspirations of many families and 
				businesses over the past two decades.<br>
				<br>
				The new logo is an evolution from the distinctive form of an 
				earlier one and bears testimony to the company’s pioneering 
				status, consistency, dynamism, customer focus, improved products 
				and the innovation that differentiates MF in this industry. The 
				brand promise of ‘Enabling Dreams’ is a simple yet evocative 
				phrase that captures the company’s proactive role in the 
				improvement of lifestyle of local families as well as helping 
				small businesses and enterprises to develop and compete in the 
				business environment of Oman.<br>
				<br>
				<b>Milestones</b><br>
				The major shareholders at present are the Omzest Group, Al 
				Yousef Group and Al Araimi Group. Having pioneered vehicle 
				financing, MF has many firsts to its credit, like being the 
				first to venture into consumer durables financing and equipment 
				leasing. The company was also the first in the Middle East to 
				launch debt factoring. <br>
				<br>
				MF has a vision to be the most preferred finance company in the 
				industry by consistently enhancing value to all its 
				stakeholders. It seeks to contribute to the development of the 
				national economy through innovations in financial products and 
				application of new technology.<br>
				The hallmark of MF operations lies in its foresight, innovation, 
				adaptability to change and its skilled Omani and expatriate 
				human resources who have a combined expertise of more than 850 
				years of experience.<br>
				<br>
				The company’s growth reflects the growth of all the clients 
				whose hopes and aspirations it has helped to realise. It is a 
				matter of great pride that it has more than 80,000 customers who 
				have made the company what it is today. To date, it has 
				disbursed cumulatively over RO422mn to its satisfied clientele. 
				It has a national presence through its wide network of branches 
				at Muscat, Sohar, Barka, Ibri, Salalah, Sur and Nizwa.<br>
				<br>
				Presently, the products offered by MF include corporate leasing, 
				debt factoring and working capital finance, auto finance and 
				corporate deposits.<br>
				<br>
				<b>Enviable track record<br>
				</b>According to Ajit Engineer, CEO, the company has been 
				profitable from its inception and has achieved an enviable track 
				record of uninterrupted dividend payments year on year. The 
				company has till date paid cumulative dividends of RO30mn, which 
				is more than four times the original capital infusion of 
				RO7.4mn.<br>
				<br>
				MF has a capital base of RO20.16mn and a net worth of RO26.84mn 
				as of June 30, 2012. The company has always followed a strategy 
				of remunerating the shareholders partly by way of stock dividend 
				and partly through cash returns. This strategy of plowing back 
				part of the profits, strengthens the capital base, provides 
				comfort to lenders and also helps in increasing the capacity to 
				borrow and lend.<br>
				<br>
				In 1992, MF had 15-16 employees with a portfolio of around 
				RO7-8mn. Today the company has a portfolio of RO120mn with more 
				than 115 staff members. In the early days, MF had only one 
				branch in Barka with five more being opened at Sur, Sohar, Ibri, 
				Nizwa and Salalah. Along the way, the company has grown its 
				portfolio with substantial developments and changes in 
				processes, changes in IT systems, changes in experienced staff 
				and introducing new products.<br>
				<br>
				Says Engineer, “When I came in 1992, most of the business 
				(around 99 per cent) was from car financing and remaining was 
				the consumer durable financing (household appliances). Then we 
				developed the corporate leasing product, equipment leasing and 
				slowly introduced debt factoring, working capital etc. As we 
				went on launching new products, some of the efforts which we put 
				in to broad-base our portfolio have now become the industry 
				norm. They get easily copied.”<br>
				<br>
				Continuing further, he says, “We were the first to launch 
				overseas debt factoring between Turkey and India. Such 
				pioneering efforts, process changes, system changes and a 
				conservative and strategic approach have enabled us to deliver 
				superior performance year after year both in terms of assets and 
				profits.”<br>
				<br>
				<b>Weathering competition<br>
				</b>Nowadays, all the banks apart from the NBFCs have got into 
				the auto-loan business as well, but MF has weathered competition 
				since inception and managed the challenges posed by them. Over 
				the quarter century, the company has gained substantial 
				experience from multiple business cycles which has helped it to 
				face competition from banks and other financial institutions 
				admirably.<br>
				<br>
				“Despite the economic slowdown, the Sultanate’s prudent fiscal 
				policies, economy’s robust fundamentals and domestic growth 
				drivers and the regulatory authorities’ continued close vigil 
				ensured that the economy continued on the desired growth path. 
				These measures helped us to cope with the challenges and gain 
				experience from the business cycles,” says Engineer.<br>
				<br>
				Today, MF is in an admirable position to capitalise on the 
				opportunities of the coming years which includes the 8th 
				Five-year Plan period where the government is expected to spend 
				almost RO54bn. According to Engineer, a substantial part of 
				government expenditure will be in sectors like oil and gas, 
				health, education and non-oil activities which includes 
				infrastructure projects. This is where a finance company like 
				MF, which has got the required capital, finances, skilled 
				manpower and the systems in place, can capitalise on in the next 
				decade.<br>
				<br>
				<b>Cherishable achievements</b><br>
				Muscat Finance is completing 25 years of service to the nation. 
				Your comments on this achievement<br>
				Muscat Finance is the first finance company incorporated in the 
				Sultanate in 1987 and licensed under the banking laws as a 
				non-banking finance company. The remarkable achievements of MF 
				over the past 25 years are thanks to the vision of HE Dr Omar 
				Zawawi, who is the main founder of MF and the first Chairman of 
				the company. Our present strategy today continues based on the 
				vision laid down by HE Dr Zawawi. On this special occasion of MF 
				celebrating its 25th anniversary, I would like to wish HE Dr 
				Zawawi long life and more prosperity. I also convey my warm 
				wishes to all the shareholders including our major shareholders 
				the Omzest Group, Al Yousef Group and Siraj Investments. My warm 
				wishes also go to our loyal staff, loyal customers and our 
				dealers and bankers who are regarded as our partners in our 
				journey.<br>
				<br>
				Over the last 25 years, in the retail segment, we have helped 
				individuals and local families by providing finance to own their 
				dream car and household appliances to improve their quality of 
				life. We have also nurtured many SMEs in achieving their 
				business goals and growing up to be fairly large corporates. 
				These customer relationships are what we cherish as the true 
				achievements of serving the Omani society and economy over the 
				last 25 years.<br>
				<br>
				<b>In the coming years, what are the opportunities that your 
				company is looking forward to?</b><br>
				I believe MF is poised for more growth given the following 
				factors:<br>
				Thanks to the wise policies of His Majesty’s government, Oman is 
				one of the few countries that is expected to weather the storm 
				of economic slowdown expected elsewhere in the world and in fact 
				some readings expect Oman to experience the highest economic 
				growth in the GCC in the next two years.<br>
				<br>
				We are exploring possibility of expanding our funding options, 
				which will enable us to provide even more economic solutions to 
				our customers, thereby improving the feasibility and cashflow of 
				their projects.<br>
				<br>
				With the rise in the government expenditure as identified in the 
				8th Five Year Plan, we believe that SMEs (who are the main 
				customer focus) will benefit substantially from these projects.<br>
				<br>
				<b>Our staff and partners<br>
				</b>The Eighth Plan Period envisages substantial expenditures of 
				approximately RO54bn and a majority of this spending would be 
				requiring capital assets. We would be focussing on corporates in 
				the major areas of expenditures of the government through direct 
				marketing and mailers and conducting business events and 
				seminars addressed to mid-corporate SME clients as well as other 
				segments like oil &amp; gas, healthcare, education, infrastructure, 
				etc<br>
				<br>
				MF has been known to support small entities in their growth. 
				Your opinion on this <br>
				MF has always nurtured client relationships and many of our 
				clients have grown with us over the quarter century. We have 
				always had a decent share of the market and with trained 
				manpower, branch network and infrastructure, we are well 
				positioned to capitalise on the opportunities in the coming 
				years.<br>
				<br>
				We have nurtured many small and medium enterprises (SMEs) in 
				their growing stages by providing them finances for all their 
				needs. And of course we will continue to maintain our 
				relationships with the SMEs but the biggest challenge facing the 
				SMEs is cash flow management and housekeeping. But fortunately, 
				we have a very good management team under the CEO which helps 
				the SME clients to study the project feasibility and establish 
				the right funding option for them.<br>
				<br>
				<b>What is your opinion about the performance of the company?</b><br>
				If you see our ratios, the returns on equity is one of the 
				highest in the financial services sector. Consistency is our 
				hallmark. Not many companies in the Middle East can boast of a 
				consistent performance year after year with an upward trajectory 
				both in terms of growth and profitability. MF has, till June 30, 
				2012, generated 14.91 per cent return on average networth, which 
				is high by industry standards.<br>
				<br>
				We have been paying dividend from day one and it is 25 years of 
				uninterrupted dividends. We have distributed RO30mn of dividends 
				since our inception. And the original capital injection 
				including the rights issue which was done recently is around 
				RO7.4mn. So roughly four times the capital has been returned as 
				dividends and the current holding is also priced admirably well 
				in the MSM.<br>
				<br>
				<b>Focus on helping business clients</b><br>
				<b>What is the unique selling proposition of Muscat Finance? <br>
				</b>We refreshed our logo along with a consumer-centric tagline 
				“Enabling your Dreams” which illustrates our focus on helping 
				our retail, SME and corporate customers to fulfill their dreams 
				of growing into larger and more profitable businesses leading to 
				enhanced prosperity. Thus “Enabling Dreams” is at the centre of 
				all our endeavours and very close to our heart.<br>
				<br>
				<b>As deputy chairman, how long have you been with the company?
				<br>
				</b>I have been with the company for about three years. Apart 
				from being the vice chairman, I also chair the audit committee 
				of the company. This audit committee is responsible for clearing 
				all the results and whatever goes to the CBO. Essentially there 
				are two committees. One is the executive committee which is 
				chaired by the chairman and the other is the audit committee 
				headed by me. Each has three or four members. So the complaints, 
				the statutory audits, whetting the quarterly results all this is 
				done by the audit committee. Certain things, depending upon the 
				value, require board approval. So we also participate in that. 
				But my committee concentrates more on the back end.<br>
				<br>
				<b>How is working with MF different from other companies?</b><br>
				I am on the board of other manufacturing companies and they 
				don’t have a regulatory regime, whereas money lending is 
				controlled by the Central Bank of Oman and they track you. And 
				there are a lot of rules and regulations and CBO even has 
				inspections every alternate year and they grade you. This is 
				unique to finance industries like banks and insurance companies. 
				So that is what makes finance companies, banks and insurance 
				companies different. Whereas in manufacturing you don’t have 
				these kinds of regulations although there is competition and 
				challenges like sourcing out raw materials.<br>
				<br>
				<b>What are the challenges you face at MF and how do you 
				overcome them?</b><br>
				MF was the first to start lending. But earlier, the dealers 
				themselves were giving credit as far as possible. Quite recently 
				we were the pioneers in factoring which is a very good method of 
				financing, and at the same time, it is good business because 
				factoring loans are self liquidative. So the initial acceptance 
				for factoring was little slow but once it caught on, people 
				understood and saw how easy it operates for both the lender and 
				the borrower.<br>
				<br>
				But we do face challenges. Firstly, competition from foreign and 
				local banks as well as the finance companies. Secondly, the ups 
				and downs of the economic cycles. The third is consistency in 
				improving our performance year on year. Because you are plowing 
				back your profits, so capital is enhanced. Then, on an enhanced 
				capital you have to deliver superior returns and this poses a 
				challenge. But we have surpassed all expectations.<br>
				<br>
				Moreover, introducing new products and making profits and making 
				them work are also challenges. These are some pioneering 
				challenges. Today it is very easy to do business through a 
				product which is already in the market. But the pains and 
				challenges of introducing a new product is a task which we have 
				successfully done. <br>
				<br>
				Many of our pioneering efforts have become the industry norms. 
				Some new ones will keep coming, but the challenge is to rise to 
				the occasion and swim even in difficult waters.<br>
				<br>
				<b>Tremendous opportunities and potential<br>
				What is the split between corporate loans and car loans 
				business? Has it changed over the years?</b><br>
				Muscat Finance commenced operations in 1987 purely as a car 
				financing company. Over the years, it pioneered and introduced 
				many products to diversify its portfolio and offer a 
				comprehensive range of financial services to our valuable 
				customers. In line with the development of the economy under the 
				wise leadership of His Majesty, the company also made 
				substantial foray into corporate business. Currently our 
				portfolio is equally divided between retail and corporate. <br>
				<br>
				The corporate segment primarily consists of leasing equipment. 
				Secondly we are giving working capital financing and factoring. 
				Over the last 25 years, we have nurtured many SMEs to become 
				large corporates and we take pride in the fact that many SMEs 
				started their businesses by taking loans from us. Our corporate 
				and SME businesses are currently doing well and with the pace in 
				the economy picking up, we see a bright future ahead in these 
				segments.<br>
				<br>
				<b>There are a number of SME success stories which have happened 
				thanks to MF. What is your opinion about SME financing?</b><br>
				MF is of the strong belief that SMEs play an important role and 
				are key contributors to the economic growth of any developing 
				economy. With substantial infrastructure development taking 
				place in the country, there is a tremendous potential to set up 
				new business and expand the existing ones. In view of this, we 
				take pride in having clients who have started businesses with 
				their first loan from MF and have grown over the years.<br>
				<br>
				<b>What are your competitive advantages?</b><br>
				Some of our competitive advantages include the fact that we are 
				the oldest NBFC in Oman and pioneers in hire purchase, leasing 
				and debt factoring in Oman. Among other advantages, we have a 
				well qualified and trained manpower and a well established and 
				well connected branch network in all the six regions of Oman. 
				Our brand image, a large customer base and our customer loyalty 
				are second to none.<br>
				<br>
				<b>How long have you been with MF?</b><br>
				I am currently the chief marketing officer of Muscat Finance 
				with overall responsibility for marketing. I have been with 
				Muscat Finance for about three years and my focus is to grow the 
				company by making it more consumer-centric. After my post 
				graduation in marketing and finance from the Indian Institute of 
				Management, Calcutta, I have acquired more than 25 years of 
				experience in financial services including various senior 
				leadership assignments across diverse businesses covering all 
				areas of operations. My previous assignment was as vice 
				president and business head with a global leader in banking and 
				financial services and prior to that as CEO of a finance company 
				in India.<br>
				<br>
				<b>Human resources important for us<br>
				As one of the founding member of Muscat Finance, how do you feel 
				after completing 25 years? <br>
				</b>I joined the company in 1987 as manager, administration. We 
				have worked hard to bring up the company to the present level. 
				When MF was established it was just for hire purchase of 
				vehicles then we branched out into other activities like 
				electronic and furniture financing. Since the inception of the 
				company, I have been heading the credit and administration 
				functions of the company.<br>
				<br>
				Prior to MF, I served 16 years in the Royal Oman Police as a Lt 
				Colonel and was the Dy Commander and Acting Commander of Police 
				Academy, Nizwa; Dy Commander of Purchasing &amp; Supply; Commander 
				of Hafeet Division; Commander of Seeb Division and Dy Director 
				General of Operations.<br>
				<br>
				<b>Omanisation is both an opportunity and a challenge. Your 
				comments</b><br>
				MF clearly recognises and accepts the fact that human resources 
				is our most important resource and asset. This is reflected in 
				the quality and skills of our staff which always command a 
				premium in the market. The board of directors (BOD) formed an HR 
				committee which reviewed the existing practices and provided 
				directions in enhancing skills, retentions and developing the 
				human resources of the company. In consultation with an external 
				HR consultant, the BOD implemented the following:<br>
				Introduced employee grading structure with increase in salary, 
				bonuses and introduction of COLA<br>
				<br>
				Training and development plan for Omani nationals (two batches 
				of 20 Omanis trained for an extensive 10-week programme at the 
				College of Banking &amp; Financial Studies)<br>
				<br>
				Educational scholarship schemes to encourage Omani nationals to 
				acquire a graduate degree and equip them with skills and 
				knowledge to take managerial responsibilities<br>
				Covered all Omani nationals under medical insurance scheme<br>
				<br>
				Subsidised car financing scheme and increased the fuel allowance<br>
				Interest-free short term loan of a reasonable amount to meet 
				emergency requirements such as health, education, marriage or 
				deaths.<br>
				<br>
				The above initiatives and on-going welfare activities for our 
				Omani staff have helped sharpen their skills, and improved their 
				performance as well as ensured retention and loyalty. The 
				company has always adhered to the directives of the regulatory 
				authority and policies of the government to abide by the 
				Omanisation targets set from time to time.<br>
				<br>
				In terms of Omanisation, we are at 78.69 per cent right now. 
				But, from the beginning, we have aimed to keep the percentages 
				higher than what is required by the government. When the 
				ministry of manpower introduced green card, we were among the 
				first to get that card, thanks to our Omanisation percentages.<br>
				<br>
				<b>Do you face any challenges like employee turnover?</b><br>
				The government has announced a number of job vacancies and the 
				people are hunting for those jobs. In government they may have 
				less salary but they look at timings because they finish at 2.30 
				pm and then they can go and play football. And also the odd 
				weekends are a hindrance. If they feel that they are getting 
				better facilities somewhere else, they will not stay.<br>
				<br>
				<b>What attracts employees to MF?</b><br>
				The most important thing is our reputation of being the pioneer 
				in the non-banking financial sector.</td>
