	<tr>
						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Meet the future halfway</font></b><p class="style15">
				<span class="style14"><strong>Excerpts from the speech of HE Ali 
				Bin Masoud Al Sunaidi, Minister of Commerce and Industry, at 
				Oman Debate 2012</strong></span><p align="center" class="style13" dir="ltr">
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-jan-13-1.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="400" height="301" hspace="7"></font><p align="justify" class="style13" dir="ltr">
				Let 
				me first thank His Highness Sayyid Tarik bin Shabeeb and the 
				organisers of this event for lining up such a distinguished list 
				of panellists. And let me first refer to what I have seen as I 
				drove to Al Bustan Palace Hotel today. I saw a young Omani drive 
				a gas cylinder vehicle and I thought he would not have delivered 
				cylinders on time and with this punctuality had he been an 
				employee of a gas company. In his case, owning and managing a 
				company which is a truck made the difference. And many people 
				would subcontract from big producers of gas. This is a an 
				example of good partnership.<br>
				<br>
				A few days ago, I was talking to my friends about a finance 
				institution. They said at 2 o clock in the night they have 
				noticed something suspicious going on electronically at one of 
				the floors of the building. That institution has subcontracted 
				its software security to a company made of 15 intelligent 
				people. My friends called the guards and asked them to check 
				what was happening on their fourth floor. They came back saying 
				that nobody was there. Apparently what had transpired someone in 
				that institution had forgotten to switch off a printer and 
				someone from outside had managed to hack into the memory of that 
				printer and tried to read the accounts details. It was only 
				because the company had such a specialised partner that it was 
				able to stop it immediately while everybody was asleep. <br>
				<br>
				Similarly I heard about banks who don’t want to own any 
				equipment further. They would like to subcontract that. But 
				partnership is a better word than subcontracting because it’s 
				basically a relationship or exchange of service for a price. But 
				it will also add to efficiency, productivity and capacity 
				building. <br>
				<br>
				Today we have more challenges than we had in any time. Markets 
				all around the world are doing funny things. Important companies 
				are being sold for less while some rewarding companies are sold 
				for high. And we saw unemployment level rise and resources 
				deplete. What is there to do in terms of cost cutting and in 
				terms of being efficient? But I think that this is a very 
				narrow-minded look. I feel we are in an era where collaboration 
				is much more meaningful than competition. <br>
				<br>
				Yesterday we had a brilliant speech by Nawras CEO. He talked 
				about a new partnership between Nawras and WhatsApp. Why on 
				earth he wants to do that? WhatsApp is looked up as a creature 
				eating into their profit. But I think you understand at one 
				point of time that if you have to fight it out, you tend to be 
				the loser. You may make money out of the value chain, as opposed 
				to making money by making people miserably pay more for their 
				phone calls. People are willing to pay more, if they get more. 
				But they don’t want to pay more if they are getting less. And if 
				you start giving to them new ideas, new items and new ways of 
				life, people would tend to pay more for them as long as they see 
				there is something more in it for them. <br>
				<br>
				And as inflation rises, people demand to be compensated. 
				Otherwise, they change the habit. If your restaurant is 
				expensive, because things are expensive, you would prefer to do 
				fewer trips. But if they give you Internet and Wi-Fi, sometimes 
				you would say I will go and I will do something while at it. 
				This is collaboration between telcos and restaurants. I think 
				the most expensive bread and coffee ever is not the ones in the 
				gardens but in the small cafeterias by the beach in Al Qurum. 
				Why do you buy the bread and coffee today for RO5/a piece while 
				you could buy at merely 200 baisas? Because they are not selling 
				you goods any more, but they are selling you a behaviour, a way 
				of life. Why would a bread manufacture like Oman flourmills sell 
				internet? Their job is to sell bread at most competitive prices. 
				But it’s somebody else who wants to mix that into a behaviour 
				which would make people drive to a place, stay there for two to 
				three hours and spend more money. So people are becoming smarter 
				and smarter and cautious. It is only those who collaborate to 
				deliver a package of services that will win and survive for the 
				future. And it is very much related to what were are doing here.<br>
				<br>
				If you think of His Majesty’s speech on the 12th of November, he 
				underlined very clearly the importance of private sector and he 
				did so by saying that it’s not a partner in the development but 
				a partner in our social activity. So private sector does not 
				only sell the services, but it changes the pattern and 
				behaviours. And therefore it gets rewarded by selling hardware 
				and software, items and services and while so doing, you are 
				really finding the way how our market behaves. <br>
				<br>
				Although we still need more infrastructures such as ports and 
				services like airport, we need to concentrate more on what the 
				next five-year plan looks like. The next five-year plan which 
				will start developing form next year will look into the issue of 
				integration, maximising the use of what we have. And what’s 
				coming up is phenomenal. Muscat International Airport is 
				undergoing an expansion by the end of 20014 or 2015 from 6mn to 
				12mn and with a small modification it would jump to 26mn and 
				with the next terminal it would jump to 46 million. Similarly 
				Salalah is doing an expansion. It will go up to 1mn and with 
				small interference in terms of warehousing and in terms of 
				conveyer belts and some other system it could go up to 2mn. In 
				addition, four other ports will account for half a million each.<br>
				<br>
				At the same time, something phenomenal is happening in Sohar and 
				Duqm and Salalah and while those are happening a train from 
				Sohar to Muscat to Duqm is being designed. How can we cope with 
				all these? The very narrow-minded thought of working in 
				competition will not take us anywhere. The narrow-minded divide 
				between government services and private sector will not take us 
				any further. And most importantly, this inward looking family 
				business will also not take us anywhere either. The silos where 
				people have protected their businesses, where the government has 
				protected itself against the private sector’s intrusion survived 
				for 42 years and may survive for another four years but it 
				cannot take us anymore. It simply cannot take the burden any 
				more.<br>
				<br>
				What’s coming is far more immense and far more complicated. 
				Today when we need to make a decision on the fisheries port in 
				Duqm, many questions come out: Is the road ready? Is the private 
				sector interested in fresh fish; and if so, will they use the 
				port in Duqm? Are we ready to give enough people to maritime 
				business? What about sea-land relationship? Look at what is 
				happening in the Gulf? Almost all the projects require a 
				combination of knowledge about the land and the sea. Corrosion 
				is a big issue. Almost all plans are coming up by the sea 
				because we cannot produce electricity without water. People who 
				know about excavation know nothing about corrosion. They wasted 
				all their life by digging mountains. They need to bring a new 
				set of people who know about corrosion, instead of wasting money 
				by thinking that they will be the most intelligent agency and 
				know it all- and there is not know-all agency anywhere in the 
				world now- neither government nor private companies. And the 
				only way you could bid is by being more intelligent.<br>
				<br>
				In the beginning of the year, I was in Silicon Valley. When I 
				went to a university, I saw a company which did a presentation 
				on a project in the gulf where they were able to reduce the 
				amount of the steel in that building by $5mn. And therefore I 
				hope that we would discuss these issues in the debate. As 
				regards interlinking the government and private sector, we would 
				have to be honest to each other and we would have to say what 
				would streamline both ends of the equation. Where is the 
				inefficiency of the government or its short falling? For 
				example, should the Ministry of Commerce privatise the one-stop 
				shop? May be they should. May be some of the government services 
				are not to be meant to stay any more with the government. And 
				similarly, should every one of you have your own trucking fleet 
				and kitchen? Shouldn’t we let smaller companies concentrate on 
				such issues? I think this multi-tasking, multi role playing 
				either between government and private sector or among private 
				sector entities need to be addressed, if you need to stay 
				competitive.<br>
				<br>
				I would think this debate would look into the issue of how to 
				retain Omanis within a company, as opposed to finding ways to 
				convincing the Ministry of Manpower that Omanis do not stay. 
				Statistics show that Omanis do not stay. But there is a company 
				in Sohar who has 700 Omanis and 300 non-Omanis who stayed. They 
				must be doing something there or they must be fools. But I am 
				sure they have proved themselves to be efficient. This company 
				is one of the hardest place for one to come to work, but their 
				accident rate is almost zero and absenteeism near to zero. So 
				statistics are dull. It only tells how inefficient you are in 
				dealing with the situation. Recently I talked to the CEO of a 
				company. They don’t have a driver in their payroll. All the 
				drivers own the truck and they look like the owners of the 
				company. And I think you feel proud to say that I am an owner of 
				the company because nobody asks you whether your company is a 
				truck, just like the guy I saw this morning selling cylinders. 
				Nobody would make an attempt to ask him: If you are an owner of 
				such and such a company, how many things you have? How many cars 
				you have? And I tell you, if he makes a thousand rial per month 
				I don’t think he will continue to drive a truck. I think he is 
				making much more than you or the government can give him in 
				terms of salary.<br>
				<br>
				We could go more and say how we encourage young entrepreneurs to 
				do things that we otherwise used to do. The number of unemployed 
				in Oman is not shocking and soon the figures will be released. 
				You would see that is not a big issue. Training and education 
				are to be readdressed. If you say for so many years that the 
				government was not able to deliver the right number of people at 
				the right time with the right quality, why don’t you own and 
				manage your own training institute? Haven’t we done so well in 
				the banking sector because of their institute? Haven’t we done 
				so well in healthcare sector because of their institute? Why 
				don’t transporters have their institute? And why don’t logistics 
				have their institute? And why don’t manufactures in every 
				industrial estate own or co-own with government their institute?
				<br>
				<br>
				And incidentally, this was a discussion of yesterday’s meeting 
				between government officials and private sector, and a few of 
				people sitting now in this room were part of it and some of them 
				have recommended this. I think instead of coming to say I will 
				throw the ball at you and waste time worth two days, we need to 
				say how we could do it better. And we need to put in black and 
				white which time to do that. Because we don’t do it, we will do 
				the same thing over and over again the next five-year plan. And 
				now we are at a very good time, because we started to think of 
				the next five years plan, and people concerned will start 
				meeting next year.<br>
				<br>
				How could we engage the public and private sector continue to 
				have a fruitful dialogue? I think this is also something to be 
				done. For a long time we had only one-way communication. I 
				attended several sessions where private sector talked and 
				government listened and many other meetings where government 
				talked while the private sector had to listen. Until today one 
				can say that this is a dialogue that is worth engaging in. But 
				that was not a dialogue, but ‘I demand you give me’ or ‘you 
				demand I give you’ kind of things. And I feel this debate for 
				the last few years has done just that- I mean engaging people on 
				a dialogue where when you go back someone brings a piece of 
				paper and say this is what people said and we think some of them 
				are worth taking forward and some of them are not. Some 
				filtering has to be there because everyone says what he thinks 
				(and I know many people also want to be movie stars and they 
				think because someone would want them to think so). Perhaps we 
				need to rethink because that does not take us anywhere.<br>
				<br>
				Issues like automation are very important. It actually breaks my 
				heart when I see five men drag a rope to take five blocks of 
				cement to the second floor. It’s not to say that productivity 
				and efficiency are already thrown out of the window when you do 
				that. People will pay slightly higher if they realise that most 
				people do that way. So automation is an issue. It also breaks my 
				heart when I see two people jump onto a big truck to take 
				cartons on their backs, when the cost of the machine doing this 
				– a lift or robot – is only RO2000. Compared with the investment 
				of a truck of RO40,000 or RO30, 00, RO2000 is nothing. It makes 
				the guy who works in the truck happy and finally when you do 
				automation you allow the person to change his designation. So he 
				is no more called a driver and he is no more called a Hammal 
				(porter) but he is called a sales agent or a distributor; we 
				know that in the next three years many a designation will vanish 
				from Oman. It’s already happening. Secretaries are no more 
				called secretaries. Watchmen are no more watching. He is much 
				more intelligent than that. He is analysing systems; checking 
				the behaviours of equipment. It’s man and man-machine 
				relationship. He is no more a guard. He uses much more 
				sophisticated system. He uses his phone to track what is 
				happening in the building. So could you call him a guard?<br>
				<br>
				We are moving to a logistic centre. We are redesigning the north 
				of Al Batinah and connecting it to the south of Batinah 
				vis-à-vis a train system. The private sector will do things on 
				behalf of the government while this system is in place. But the 
				point is that if we do that, do we still need to bring more 
				labour from outside? If that is the case, the issue of 
				Omanisation will become a fallacy. I know that there are at 
				least four companies in that area alone doing smart ware houses 
				taking online orders and doing labelling, billing etc. and some 
				of them can use robotics in this. They are thinking that things 
				cannot go the way they used to be.<br>
				<br>
				You should not underutilise space and you shoud not underutilise 
				the capital and human capital. People are thinking again and 
				again of being more productive and efficient more than ever, 
				because the challenges ahead of us everywhere in the world would 
				mean that you have to compete with other people who are far 
				cheaper than you either in the delivery or in the experience of 
				delivery. The only thing is that you have to control. I could 
				not take an aeroplane to go to china to have a cup of tea and 
				come back although it is cheaper. You still have to have your 
				coffee here.<br>
				<br>
				We need to seize this opportunity for rethinking what we should 
				do in the next 20 years with this entire infrastructure that is 
				underutilised, instead of saying we are not doing it right. With 
				a new exhibition centre what we should do? I agree that the 
				government will have to take certain blame because we need to 
				get ourselves sorted out. As regards Omanisation of labours and 
				drivers, do you think that when you take them out of your 
				payroll you will fall short of the number and therefore the 
				Ministry will not give you new labourers? We need to build trust 
				among ourselves .Why don’t the Ministry of manpower trust you 
				and give you more labour because they see most of them doing job 
				they should not do. So I trust you and you trust me so that I 
				can understand that the you are asking for extra person because 
				you need them. I think these are the issues we need to discuss- 
				tools of building trust.<br>
				<br>
				People would say that productivity, innovation, efficiency, 
				worker motivation are mere luxury items so that they would only 
				come to them from time to time, but the only things they 
				concentrate on are expanding sales and cutting costs. But I 
				think people want to stay ahead of the game. They need to think 
				again. These are no longer luxury. So I think some of these 
				issues will pop up in the first debate and some of them that 
				relate to using and making better use of the ports, the big 
				facilities you are building, will come up in the second session.<br>
				Thank you very much.</td>
