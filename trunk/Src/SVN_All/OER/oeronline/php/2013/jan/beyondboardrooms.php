						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Gateway to aquatic wonders</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>The recently launched 100-birth marina at Jebel Sifah by 
				Muriya Tourism Development offers a spectacular opportunity for 
				water enthusiasts to explore a raft of uncharted splendours in 
				the sea of Oman. Muhammed Nafie reports</b><p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/beyondboardrooms-jan-13-1.jpg" hspace="13" vspace="3" width="300" height="224"><p align="justify" class="style13" dir="ltr">
				<br>
				Have you had enough of driving around Oman? Tired of exploring 
				by road the grandeur and beauty of the Sultanate’s mountainous 
				terrain? But its rugged and idyllic coastlines still hold a lot 
				in store for you. Marinas coming up in different parts of the 
				country offer tremendous opportunities for water enthusiasts to 
				visit various uncharted splendour of Oman in the sea, including 
				stunning natural bays and beaches, amazing snorkelling, diving 
				and fishing spots, isolated islands tucked away amidst gorgeous 
				rocks etc.<br>
				<br>
				The recently launched 100-birth marina at Jebel Sifah by Muriya 
				Tourism Development serves as a gateway to many of such lovely 
				locations at the Gulf of Oman such as Bandar Khayran and islands 
				of Ras Abou Daoud. Set to transform the area into an exotic 
				locale, the marina is the heart of the integrated tourism 
				complex that Muriya is setting up at Jebel Sifah. Apart from 
				natural beaches, bays and Island, those who have a love for 
				nautical adventure are offered an opportunity to experience 
				snorkelling, diving and fishing.<br>
				<br>
				“The marina not just offers berthing facilities but also 
				presents an opportunity to explore destinations that are located 
				at close proximity,” sated Philip A Jones, CMM, senior director, 
				marinas. “The natural beaches along with the majestic mountain 
				backdrop add to the overall allure of the place.” He also 
				informed that there was a special offer until the end of 2012. 
				The first 10 early birds to book berthing space would get two 
				years of free berthing while the next 10 bookings would get one 
				year free.<br>
				<br>
				The marina can accommodate various types of boats from smaller 
				boats up to larger six-metre boats. Philip said that most of the 
				smaller boats required pontoons in order to facilitate getting 
				on and off the boat. “In the coming months, the marina will 
				offer 360º services such as boat management, engines 
				maintenance, repairs and much more,” he added. “An option of dry 
				or wet berthing facilities will also be available.”<br>
				<br>
				Philip informs that there are a lot of recreational activities 
				planned to attract more water enthusiast to enjoy the facility 
				such as fishing competition. We have also set up camping areas 
				on the beaches where youngsters can come and camp.<br>
				<br>
				The marina also provides an interesting opportunity to travel 
				halfway by water and halfway by road to some of the interesting 
				destinations in the country. For instance, a 20-minute cruise 
				from Jebel Sifah will take you to Quriyat from where you can go 
				to the spectacular Wadi Shab in another 20 minutes by road.<br>
				<br>
				As the project aims to connect the capital area by water in the 
				long run, it will help the sightseers to make a hop on and off 
				trip to various scenic spots both on and off the sea. As Philip 
				exudes confidence, sooner or later, we will be able to make a 
				round-the-Muscat cruise starting from Jebel Sifah to Seeb with 
				stopovers at Shangri-La, Al Bustan, Marina Bandar, The Wave 
				Muscat etc.&#8194;</td>
