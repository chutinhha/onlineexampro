						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						United for a cause</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						The third edition of Beat Diabetes Walkathon marked the 
						culmination of a year-long diabetes awareness drives and 
						activities led by the Landmark Group<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/health-dec-12-1.jpg" width="500" height="332"><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
                		Around 6000 people dressed in blue joined the third Beat 
				Diabetes Walkathon, organised recently by the retail and 
				hospitality conglomerate, Landmark Group. The walk was flagged 
				off from Centrepoint, Ruwi with an aim to spread awareness and 
				beat diabetes in the Sultanate.<br>
				<br>
				The 2012 edition of Walkathon in Oman was organised under the 
				patronage of the Ministry of Health and Oman Diabetic 
				Association and marked the culmination of a year-long timeline 
				of diabetes awareness drives and activities led by the Landmark 
				Group.<br>
				<br>
				Commenting on the occasion Clive Freeman, COO, Landmark Group 
				Oman said, “Our aim is to spread awareness about diabetes and to 
				give people enough knowledge on the condition to help them make 
				informed choices on eating and living healthy and also encourage 
				people to regularly test their blood glucose level.” <br>
				<br>
				<b>Major threat<br>
				</b>The number of diabetic patients is escalating day by day all 
				over the world and in the Sultanate the number of Omanis living 
				with diabetes and registered at MoH institutions surpassed 
				170,000. The diabetes prevalence among adults is currently 
				estimated at 12.3 per cent which slightly increased from the 
				earlier reported figure in 2000. On the other hand, Diabetes 
				continues to bleed the budgets of all health care systems 
				threatening sustainability if preventive measures are not 
				implemented.<br>
				<br>
				Dr Noor Al- Busaidi, president of Oman Diabetes Association was 
				present at the occasion and was glad to be a part of the walk. 
				“I’m extremely grateful to everyone who has gathered here for a 
				social cause,” said Dr Noor. “Diabetes has become a very serious 
				concern across the globe and especially in Sultanate, where the 
				incidence of diabetes is extremely high. This is a result of a 
				lot of factors put together – unhealthy and sedentary lifestyle, 
				poor eating habits, lack of exercise. With the support of the 
				masses we can spread awareness and beat diabetes and live a 
				healthy life.”<br>
				<br>
				Ali Mallalah Al Lawati said, “We hope that this initiative will 
				usher in a positive change in people’s mindset about diabetes. 
				We can make a start from this moment and reflect upon our 
				lifestyles and commit ourselves towards making a change to lead 
				a healthier life. Our combined efforts will spread the word and 
				we will ‘Beat Diabetes’ together.<br>
				<br>
				The walkathon was preceded by a month long awareness campaign 
				led by Landmark Group where over 1000 free blood glucose tests 
				were done all over Oman. The campaign was actively supported by 
				Al Raffa Hospital and Johnson &amp; Johnson – Lifescan. Nissan from 
				Suhail Bhawan Automobiles extended their support during the 
				registration drive for the walkathon across the Capital area. 
				TRA, Oman Oil Company and Renaissance Services supported blood 
				testing camps which was the part of the campaign.<br>
				<br>
				Landmark Group encourages people to regularly monitor their 
				blood glucose levels to ensure early detection and better 
				management of diabetes. Through its on-going campaigns such as 
				‘Beat Diabetes, Eat Healthy’ and ‘Beat Diabetes, Get Active’ the 
				Group actively seeks opportunities to raise awareness about the 
				condition.&#8194;</font></td>
