				<img border="0" src="images/browsingcorner-jan-13-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3" width="130" height="208"><b>Think 
				Big Act Small-How America’s Best Performing Companies Keep the 
				Start-up Spirit Alive by Jason Jennings is an insightful work 
				examining the success of nine amazingly profitable and well-run 
				companies in the US who simply defied the conventional wisdom of 
				thinking. An OER review</b><br>
				<br>
				Each year millions of businesses are founded worldwide. Sometime 
				it’s an individual going solo in a home office. In other 
				instances it’s friends and partners pooling resources to pursue 
				a dream. And other times it’s the launch of a manufacturing or 
				service facility designed to employ thousands. In the case of 
				every new business there are two certainties: there was one big 
				thinking behind the decision to make the leap.<br>
				<br>
				Tradition says there are three ways to grow a company’s profits: 
				Fire up the sales team with empty promises, cut costs and 
				downsize, or cook the books. But what if there’s a better way – 
				a way that nine amazingly profitable and well-run companies are 
				already embracing? Jason Jennings and his research team screened 
				more than 100,000 American companies to find nine that rarely 
				end up on magazine covers, yet have increased revenues and 
				profits by ten percent or more for ten consecutive years. Then 
				they interviewed the leaders, workers, and customers of these 
				quiet superstars to find the secrets of their astoundingly 
				consistent and profitable growth.<br>
				<br>
				Offering engaging case studies of nine of America’s best 
				performing (if unfamiliar) businesses, Jennings identifies 10 
				practices they all have in common, which, he argues, catapulted 
				them into the rarefied category of increasing profits and 
				revenue by 10 per cent or more for at least 10 consecutive 
				years. They cut across a wide spectrum of enterprises, but all, 
				according to Jennings, have “nailed the fundamentals.” Ten 
				bullet-pointed and chart-summarized chapters with prescriptive 
				titles present the basics that these prosperous business have 
				mastered and asserts that others who apply the principles will 
				also fatten their bottom lines.<br>
				<br>
				What they have in common is a culture – a community – based on a 
				shockingly simple precept: Think big, but act small. It works 
				for retailers like PETCO, Cabela’s, and O’Reilly Automotive, 
				manufacturers like Medline Industries, service companies like 
				Sonic Drive-In, private educational companies like Strayer, 
				industrial giants like Koch Enterprises, and software companies 
				like SAS.These companies think big ideas about solving 
				customers’ problems, making better products, and creating value.<br>
				<br>
				As Jennings observes in the introduction of the book, thinking 
				big has been responsible for every advance, every development, 
				and every successful business known to man. There were big 
				thinkers behind rocket ships, skyscrapers, computers, assembly 
				lines, fast-food hamburgers, and every leap forward in medical 
				science. Along the way, the market place has handsomely rewarded 
				many big thinkers. Bill Gates, Warren Buffett, and Sam Walton 
				all defied conventional wisdom, thought big, significantly 
				changed the world, and amassed fortunes worth tens of billions 
				of dollars in the processes.<br>
				<br>
				In breezy prose with plenty of anecdotes from CEO and worker 
				interviews, Jennings argues that regardless of how big a company 
				becomes, acting big and ignoring the needs of employees, 
				merchants and customers always leads to lost profit. Concluding 
				sections offer business self- evaluation materials, fascinating 
				background on research methodologies.&#8194;</font></p>
