						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">Transforming the Primary 
						Sector</span><span class="style14"><br>
						<br>
						<br>
						</span></strong><b><font face="Verdana" size="1">
						<img border="0" src="http://www.oeronline.com/images/oliver_cornock.jpg" width="150" height="186" align="right" hspace="8" vspace="3">During 
						2010-11 the government has invested over $360mn towards 
						modern systems of irrigation, directed at changing 
						patterns and methods of traditional farming, introducing 
						greenhouses and other new farming technologies with a 
						view to increasing yields<br>
						<br>
						By Oliver Cornock</font></b><br>
						&nbsp;<p align="justify" class="style13">
						While oil might be occupying the headlines in Oman, the 
						past year has seen remarkable growth in one of the 
						Sultanate’s more traditional sectors. Indeed, the 
						Ministry of Agriculture and Fisheries (MOAF) is now 
						looking to develop the primary sector – agriculture and 
						fisheries – as one of the most important pillars of 
						economic and social development. Aquaculture, in 
						particular, has been identified as having the capacity 
						to not only offer a major contribution to GDP, but to 
						provide employment for around 11,000 people by 2020, 
						according to MOAF estimates.<br>
						<br>
						The Sultanate has a centuries-old tradition of fishing, 
						but until now both aquaculture and agriculture have been 
						relatively small scale, low-yield activities. Yet a 
						combination of necessity and untapped potential are 
						behind the government’s prioritisation of primary sector 
						activity.<br>
						<br>
						<b>Demand for food<br>
						</b>Omani agriculture, from arable to pastoral farming, 
						is not currently yielding the margins it might. Just 0.1 
						per cent of the country is currently used for arable 
						farming, while the Sultanate is blessed with long 
						stretches of coastal waters rich in supplies of fish and 
						shrimp, resources which remain largely unexploited. 
						Instead, there has been an increasing dependence on 
						imported food. Last year, research published by the 
						Economist Intelligence Unit estimated the value of food 
						imports to Oman would more than double over the next 
						decade, to $4.8bn by 2020. Indeed, over 80 per cent of 
						demand for food in Oman is met through imports, 
						according to the same study. This reliance on food 
						imports is not only costly, but it offers no long-term 
						solution to the issue of food security – an increasing 
						concern as Oman’s population grows. Moreover, with food 
						inflation for the GCC region forecast by the QNB Group 
						at 4.1 per cent for the remainder of 2012, and predicted 
						to possibly spike again in 2013, domestic food supplies 
						will become increasingly important in preventing another 
						“global food crisis”, such as was seen in 2007, from 
						reoccurring.<br>
						<br>
						Oman is far from alone in its dependence on imports, and 
						the GCC average is estimated to be slightly higher, at 
						around 90 per cent – equivalent to 51 million tonnes of 
						food for the period 2011-15 – according to food event 
						organiser SIAL Middle East. But unlike its neighbours, 
						the Sultanate believes it has untapped natural resources 
						which can enable it to become vastly more 
						self-sufficient.<br>
						<br>
						In order to capitalise on this potential, and raise 
						levels of production within its farming industries, the 
						government has adopted a strategy of improving farming 
						infrastructure and developing new regulations to oversee 
						the sector. Most noticeably, the government has invested 
						over $360mn towards modern systems of irrigation, 
						benefitting 398 farms and covering 21,700 acres, during 
						2010-11. More specifically, this investment has been 
						directed at changing patterns and methods of traditional 
						farming, introducing greenhouses and other new farming 
						technologies with a view to increasing yields.<br>
						<br>
						Furthermore, the training and technology involved in 
						agricultural expansion and development is having a 
						positive effect on Omani society, especially given the 
						government’s approach has been to work from the bottom 
						up, imparting knowledge and skills among the rural 
						population. One such MOAF project, which concluded in 
						September, 2012 focussed on the social advantages of 
						helping women generate a source of income, by 
						establishing small pastoral farm manufacturing units for 
						milking cows, as well as separating and hatching chicken 
						eggs. The MOAF found that not only did the use of such 
						equipment result in marked efficiency improvements 
						(compared to traditional, manual processes) but it also 
						boosted family income by around 30 per cent, with the 
						financial status of an estimated 326 families improved.<br>
						<br>
						<b>Extending cultivable land<br>
						</b>While only a small proportion of Oman’s largely 
						desert countryside is used for farming, it is hoped that 
						the Sultanate can extend the area of land suitable for 
						cultivation through a combination of irrigation, pest 
						control and crop diversification. With such measures in 
						place, the expansion of wheat production has become a 
						possibility, and the MOAF is now planning to plant an 
						additional 230 acres during the coming season. In July 
						2012 it was also reported that a new programme to expand 
						the distribution of palm seedlings, as well as fruits 
						such as mango and lemon seedlings, was set to get 
						underway, although further details have yet to be made 
						available.<br>
						<br>
						It seems bigger plans are in store for aquaculture. 
						Replacing the traditional fishing boats to make way for 
						more efficient fishing of the region’s abundant marine 
						wealth, the government is hoping to turn aquaculture 
						into a major GDP contributor. Indeed, the extent of 
						coast available for aquaculture stands at about 15,000 
						ha – some 3000 ha of which has already been approved by 
						the MOAF for immediate development.<br>
						<br>
						At the time of writing, the bidding for the contract to 
						develop the Duqm Fishing Harbour project was nearing the 
						closing period. The project, which is set to be an 
						integral part of the Duqm Special Economic Zone, is 
						designed to enhance the commercialisation of the 
						region’s prodigious fishery resources, and will offer 
						fishing vessels almost 3.5km of breakwater arms, as well 
						as facilities for repair work, berthing and mooring 
						aids. With such facilities in place, it is hoped there 
						will be an increase in private fishing enterprises 
						coming to the area.<br>
						<br>
						This development is part of a government investment 
						programme amounting to $100bn, but the sector is also 
						expected to create opportunities for private sector 
						involvement amounting to a further RO320mn by 2030. 
						Indeed, the government hopes Duqm will help catalyse the 
						creation of a major fishing industry. Fahad Ibrahim, 
						director of the Aquaculture Centre at the MOAF, has said 
						he expects the overall sector to contribute $2bn to the 
						gross domestic product by 2030. Moreover, it is hoped 
						that aquaculture will have the capacity to generate jobs 
						for Omanis, to the tune of 50 jobs for every tonne 
						produced by aquafarming and its associated activities. 
						And so far, the figures suggest that raid growth is 
						already afoot; while output for the sector stood at 127 
						tonnes (combined shrimp and fish catches) in 2010, that 
						figure rose to 251 tonnes in 2011, according to the 
						MOAF.<br>
						<br>
						<b>New laws for aquaculture farming<br>
						</b>To stimulate private investment, the Minister of 
						Agriculture and Fisheries Wealth has also announced that 
						a new regulatory framework for aquaculture farming. 
						These new bylaws have been designed to facilitate 
						investment with guidelines that enable foreign investors 
						to enter the market easily. Whereas in the past activity 
						within the segment was largely unregulated, 
						clearly-defined rules have now been outlined regarding a 
						governing body for the industry, environmental concerns, 
						threats to human health, sustainable growth and 
						promotion, licensing, fees, investment guidelines and 
						quality controls. Furthermore, government incentives for 
						farmers and investors include soft loans, no tax on 
						imported equipment and farm inputs, and no income tax 
						during five years.<br>
						<br>
						With these laws in place, the MOAF anticipates local and 
						foreign investments of around $100mn during the first 
						round of announcements and project tenders, which were 
						ongoing at the time of writing.<br>
						<br>
						Whilst the figures may be relatively small in comparison 
						to Oman’s recent oil revenues, agriculture and 
						aquaculture, taken together, offer rich potential for 
						employment and improved food security and, consequently, 
						sustainable alternative growth drivers.&#8194;</td>
