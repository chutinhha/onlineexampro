						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Make a Plan</font></b><p class="style15">
				<span class="style14"><strong>The second session of Oman Debate 
				2012 titled ‘Freezones and Industrial Areas: Powerhouses of 
				tomorrow- Opportunities and Challenges’ witnessed spirited 
				deliberations on the accountability of the government and 
				corporates, transparency, industrial relations, reformation of 
				labour law etc. Excerpts</strong></span><p align="center" class="style13" dir="ltr">
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-jan-13-3.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="400" height="217" hspace="7"></font><p align="center" class="style13" dir="ltr">
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>Tim: We will get from the panel a sense of where they are on 
				the question ‘Is Oman Future Ready’. Would you like to pick up, 
				Marcos Belucco, based on what you heard in the first session?</b><br>
				Marco: The construction of the future foundation is a continuous 
				process and the private and the government sector have to work 
				together and I am confident that Oman will be the most important 
				trade hub in the Middle East in the next five years.<br>
				<br>
				<b>Tim: What are the concerns? What could come in the way of 
				that?</b><br>
				Marco: Considering the huge investments by the government to 
				provide logistic systems, we have to invest in the education 
				sector to provide a qualified manpower for the future Oman 
				needs.<br>
				<br>
				<b>Tim: Musab, what do you have to say?<br>
				</b>Musab al Mahrouqi: In general, I think we have made 
				significant steps. But the challenge we have today is to wake up 
				every morning and think how I can be better or faster than the 
				day before. And the challenge is not really local but it is of a 
				global nature. The world is becoming faster and hence we have 
				become more demanding and more aggressive every day. Sometimes 
				the pace of development and decision-making do not happen at the 
				pace we want them to be. <br>
				<br>
				Tim: Expectations are too high…<br>
				Musab: Expectations are increasing day by day because our 
				exposures are wider and our understanding is bigger and better 
				and people are becoming more educated etc. <br>
				<br>
				Tim: But you heard reservations about the amount of unemployed 
				in the society but you say expectations are still high<br>
				<br>
				Musab: I think, with time we will learn that meeting 
				expectations is not possible. I think if we wake up in the 
				morning and think that I am gonna go and meet the expectations 
				set for me, this is unrealistic. I always tell people in my 
				organisation that 80-20 (The Pareto principle – also known as 
				the 80–20 rule, the law of the vital few, and the principle of 
				factor sparsity) is good enough.<br>
				<br>
				Tim: Okay… Peter Ford <br>
				Peter: I fully subscribe to Steve’s views in the previous panel 
				that there is a long way to go but we have around 25 per cent of 
				the base there. And as an American optimist, I think there is a 
				huge opportunity for this country and for those of us working 
				together to take advantage of that 75 per cent, and take 
				advantage of that quickly because we have so many models that 
				are working outside Oman that we can use and modify within this 
				context. We don’t have to reinvent the wheel on ease of doing 
				business, on training or education or any of these things. There 
				are some very good things within the region itself to learn 
				from. So let’s take that and accelerate it remembering that 80 
				per cent product now is better than 100 per cent product 10 
				years from now.<br>
				<br>
				<b>Tim: One of the concerns that were flagged up in the first 
				session was the lack of communication, government to people. Is 
				that something that you have come across?</b><br>
				Peter: I think they were fair comments. The previous session 
				panellists and the people in the audience also reflected that. 
				Communication can always improve. It’s one of those things you 
				can say- we want to improve communication. I don’t think 
				communication can ever be too much, to tell you the truth. It’s 
				a difficult turn at the cross, so let’s improve on that, let’s 
				have more of these debates. This is an excellent step and some 
				more of what His Excellency was saying, which is getting advice 
				from outside entities – outside the government- as the 
				government doesn’t know it all nor does each individual private 
				sector entity. But if we start sitting together in roundtables, 
				we can get a view of everything and we can say, by the way, 
				let’s look outside and see what’s working already and try and 
				modify that to our needs. <br>
				<br>
				<b>Tim: Do you understand the grievances that were expressed in 
				the beginning of this year (2012) and last year (2011)? Somebody 
				was suggesting that the people were pushed towards that because 
				the government wasn’t listening sufficiently to their demands?</b><br>
				Peter: I can’t speak about that because that was a bit before my 
				time but I can easily see that people who haven’t been listened 
				to would have concerns and would come out in protest. And I 
				think that discussion on freedom of speech and freedom of 
				expression was an interesting one. And it is interesting to see 
				it develop because there are so many slippery slopes where we 
				have to find out what is constructive and non-constructive. And 
				these are valid questions and at the same time, the vision is 
				the thing that has to prevail. Do you want a free economy and 
				freedom of expression? If so then you have to take a punt on 
				some of them.<br>
				<br>
				<b>Tim: And one relies on the other, doesn’t it?</b><br>
				Peter: Absolutely…<br>
				Tim: A free economy relies on free expression…<br>
				Peter: Clearly, clearly…<br>
				Tim: You can’t separate the two…<br>
				Peter: No, I don’t think you can<br>
				<b><br>
				Tim: Okay, Henk Pauw?</b><br>
				Henk: I pressed the button 2 to show that Oman is not future 
				ready. It also depends upon what do you mean, it’s a very open 
				question. What do you mean by “ready”. And there are Omanis out 
				there who have very high expectations of what they want to be. 
				And it was said in the previous panel. It takes time to get 
				there because this is a developing country and it has done great 
				things in the last few years and if it keeps that up, it will be 
				one of the power houses in the Gulf.<br>
				<br>
				<b>Tim: You raised a question, while talking yesterday (in the 
				briefing session) that Omanis want a lot. Are they willing to 
				work? </b><br>
				Henk: One of the issues is the culture of entitlement in the 
				sense that people want a lot of things from the government and 
				from employers etc. But what are they doing themselves? They 
				have a responsibility to themselves as well to educate 
				themselves, to learn and to go out after the opportunities.<br>
				<br>
				<b>Tim: To learn or adapt? Are there cultural mores or hurdles?</b><br>
				Henk: I don’t think so. Somewhere in the last few years, there 
				is something lost in the educational system and also on how to 
				achieve; and there was a very high need for talent, so everybody 
				got a job anyway. Now there are much more people who need a job 
				and it is not self-evident that you get one. You have to work 
				for that. In Oman, there is always family who will help you. 
				There is no poverty trap. So you don’t have examples of what 
				happens when you don’t employ yourself.<br>
				<br>
				<b>Tim: What’s different about the mindset in Oman as compared 
				to other places you worked in?</b><br>
				Henk: Yea, this is my sixth country. I think one of the 
				difficulties is in manufacturing- there is no mindset for 
				manufacturing… <br>
				Tim: (stress on words) No mindset for manufacturing…<br>
				Henk: There is no cultural heritage for manufacturing... <br>
				Tim: So lack of knowledge, lack of perspective…<br>
				Henk: I don’t say knowledge, it’s more experience. It’s knowing 
				that is needed.<br>
				<br>
				<b>Tim: To what extent do you think this is holding the country 
				back?</b><br>
				Henk: I don’t think it is specific for Oman. It is just part of 
				the growing that when you start investing in these kinds of 
				project, you have to teach. We have our own training centres to 
				help people to understand…<br>
				<br>
				<b>Tim: Has it been frustrating dealing with different mindsets?</b><br>
				Henk: No, it’s just part of being here in Oman. You have to do 
				that.<br>
				Tim: Nicholas Barakat, you seem to be concerned that there are 
				not enough partnerships between educational institutions and the 
				industry.<br>
				<br>
				Nicholas: This morning we talked about the importance of 
				education. In our view, in order to prepare the employee of the 
				future, an individual has to be able to integrate herself easily 
				into the workforce. And in many countries, what they have is a 
				partnership between universities and the technical schools in 
				order to prepare people. That is not prevalent in Oman.<br>
				<br>
				Tim: Someone needs to push that…<br>
				Nicholas: Yea, however, these links can be formed very easily. 
				For example, in Octal, in the beginning we hired 40 engineers, 
				none of which had been exposed to the industrial environment. 
				None of them have been into a factory formerly, but very quickly 
				that transition happened, especially, in the analytical and 
				qualitative positions such as instrumentation. In respect to the 
				future ready question, I think the key element here is how one 
				competes in the world market. And I think that is the real 
				question. Does Oman and individual Omani companies have the 
				capabilities and resources to compete in an open global market? 
				Over the last five years we have seen that. <br>
				<br>
				Tim: So the government should do more to promote exports…<br>
				Nicholas: Yes that’s the key part. For example, in the Salalah 
				Port, the key element now after the infrastructure is built, is 
				attracting the big shipping lines that can get us to the key 
				markets of the world. Yes, that is moving in the right 
				direction. We ship to New York in 14 days, Chicago in 18 days, 
				San Diego in nine days. Therefore, yes Oman is future ready, in 
				the sense that it has already a lot of the elements of success. 
				I don’t think we should forget that. All we have to do is to 
				build on that.<br>
				<br>
				Now, does it have all the trade agreements? For example, look at 
				the success of Korea in the past three years. The success of 
				Korea to negotiate the Free Trade Agreement with Europe is 
				phenomenal. But Oman does not have an FTA with EU although it is 
				one of the few countries to have an FTA with the US which in 
				itself is a success. But what is more important is a vote of 
				confidence by the US government that investing in Oman is safe, 
				and rights to property and rights to intellectual property etc. 
				It means that you are already part of an exclusive group. So for 
				me Oman is future ready. The trappings are there, we have to 
				build on that. <br>
				<br>
				Tim: And be a little more aggressive internationally<br>
				Nicholas: Yes, a good example is we export 99 per cent of what 
				we make. So you have to be competitive, it’s a combination of 
				efficiency, costs and access to capital. <br>
				<br>
				<b>Tim: Peter Ford, do you share the view that the government 
				has to be more aggressive internationally?</b><br>
				Peter: I think that the step that the government has taken 
				recently in allowing the ministry of foreign affairs to actively 
				market Oman is an excellent one. There is something around the 
				Free Trade Agreement which I think is an interesting concept. 
				The US FTA may be considered as a failure and a success at the 
				same time because one could wonder why there are not more 
				American businesses inside Oman starting up companies and taking 
				advantage of this. I have heard anecdotal evidence although 
				decision has been made at the higher levels. Companies are being 
				asked to provide proof of their photocopies of passports to make 
				sure it’s American. <br>
				<br>
				Tim: This is what they will ask here. So the red tape is 
				enormous.<br>
				Peter: It is getting lower but it is at an unacceptable level.<br>
				Tim: So ease of doing business is the key thing that they should 
				be working on.<br>
				Peter: It should be.<br>
				Tim: (pointing to Musab) Would you accept that? Ease of doing 
				business<br>
				Musab: The sector I am familiar with is the industrial sector. 
				Perhaps we can focus on that. I can say about the philosophy 
				behind the construction of the Port of Sohar and how the 
				industries were set up. Perhaps there are some challenges in 
				that model right now but looking at other experiences around the 
				region, we can see some benefits. There is the absence of an 
				umbrella that helps facilitates the ease of doing business in a 
				confined industrial area in Sohar. And industries still have 
				challenges.<br>
				<br>
				Tim: What about the red tape that Peter Ford was talking about?<br>
				Musab: I think that is what I am referring to. If there was one 
				umbrella and one entity responsible for facilitating doing 
				business in these industrial areas, we would probably avoid all 
				these issues.<br>
				<br>
				<b>Tim: A one-stop shop?</b><br>
				Musab: Yae, a one-stop-shop. It has to be visible; it has to be 
				active, engaged with industries. You can’t sit in Muscat and 
				then imagine things in Sohar would do well just by themselves.<br>
				<br>
				Vote on the first question of the session: Are large companies 
				serving the interest of Oman?<br>
				NO: 60: YES: 40.<br>
				<br>
				<b>Tim: Would you share that view Henk Pauw?</b><br>
				Henk: Probably some more needs be done there. We are already 
				doing quite a lot for the Omani economy, probably not explaining 
				very well what it is all. So maybe we should do a little more on 
				that front.<br>
				<br>
				<b>Tim: Nicholas Barakat. Do you agree with the vote results?</b><br>
				Nicholas: Yea, pretty much. I mean the purpose of businesses is 
				to create a better world and I think the large organisations 
				which come with instructions on which they can not only create 
				employment but create foreign exchange are beneficial especially 
				if they have done it within international norms. So, the fact 
				that Oman is able to attract capital and develop industries is a 
				good spot. This being said, these industries only make sense if 
				they can compete. They do good when they are successful.<br>
				<br>
				<b>Tim: Peter Ford, the government made good investments in the 
				Sohar Port, the Salalah Port with the aim of creating downstream 
				jobs. $14bn invested in Sohar and only 7000 jobs created. Are 
				they giving value for money or not?</b><br>
				Peter: Well, I am terribly familiar with Sohar and the 
				statistics behind that, I am more familiar of course with the 
				Salalah area. And Salalah is an interesting place because it is 
				private money or a significant part of it is private money 
				publicly traded on the stock market and so we have shareholders 
				to whom we have to add value to. And some of those shareholders 
				are actually private citizens and some of them are pension funds 
				and banks etc. To build on Nicholas’ comment, the big businesses 
				are here to add value, value to communities, government and 
				shareholders and value to staff. And we have talked a lot about 
				all those.<br>
				<br>
				When I looked at that vote and saw the high percentage of No, I 
				guess the question is what is it that the people expect these 
				projects to deliver? And is it a mismatch in expectations? Is it 
				that we thought that we are going to invest huge amounts of 
				money that would generate 10,000 jobs at a plant? And maybe the 
				issue of communication, communicating investment, not just the 
				amount but also the expectation of returns is what could solve 
				some of the mismatches that we see.<br>
				<br>
				<b>Tim: Manal, from the first panel, you are still in the 
				audience I see. $14bn invested in Sohar and only 7000 jobs 
				created. Has it been a catastrophic waste of money from the 
				government’s point of view?</b><br>
				Manal: We’ll have to realise that this area, the Sohar 
				Industrial Port area, is capital intensive. I mean we always 
				knew that the projects and industries in Sohar are not going to 
				generate that many jobs. But what we did expect is that the 
				projects in Sohar are to create downstream and create a 
				multiplier effect. Actually for every direct job, there is a 
				possibility of creating four indirect (1:4 ratio) jobs in the 
				private sector around the community of Sohar. And this is where 
				we should concentrate: how can these big industrities which are 
				represented here today be engaged in creating the second wave of 
				development in Sohar. Go through their value chain, break down 
				their procurement, and look at the possibility of setting up 
				small entrepreneurs based on their operations.<br>
				<br>
				<b>Tim: So is this working? Are large companies serving the 
				interest of the country?</b><br>
				Manal: I was one of those who voted No because there is still 
				possibility of doing more. And I think this has come to the 
				attention of the business community in Sohar. Most of them have 
				programmes like in-country value and have discussions with their 
				suppliers and contractors to see how they can add value to the 
				economy.<br>
				<br>
				<b>Tim: Musab Al Mahrouqi, is it fair when people accuse 
				companies of taking advantage of cheap gas, infrastructure, 
				access to world class ports and not providing enough jobs for 
				Omanis?</b><br>
				Musab: The question is what is enough… <br>
				<br>
				<b>Tim: The question is, is the accusation fair? What do you 
				think?</b><br>
				Marcos: I think the industries happened in Oman for a reason. I 
				think we wanted to monetise the resources we have in the 
				country. I think we followed a general industrial development 
				that was not only happening in Oman but all over the region. The 
				whole region was industrialising the hydrocarbon sector in 
				different ways to monetise the available oil and gas. And Oman 
				was no different. I think we had very successful projects some 
				of which were facilitated by the government while in some others 
				government is a shareholder as well as a beneficiary. <br>
				<br>
				Tim: So you are telling me that it is a mixed picture. You don’t 
				wanna come down one way or the other.<br>
				<br>
				Musab: I think we are too young as an industry to make a 
				judgement whether it has worked well or not.<br>
				<br>
				Tim: But you need to make judgements to see whether you are on 
				the right track or not after putting in so much investment<br>
				<br>
				Musab: I am for putting in more investments in the hydrocarbon 
				sector if that is what you want to hear. If you think of what 
				Oman can do in the future, we think about what we have, and what 
				we have is limited resources focused on oil and gas, a natural 
				resource and hence if we want to develop, industrialise and 
				create economic development, we have got to think what we have. 
				Of course location is one and building ports is the other<br>
				<br>
				<b>Tim: Are you talking about diversification?</b><br>
				Musab: The word diversification is a very difficult one. 
				Diversification from what to what? This is a different debate 
				altogether. So… who is diversified today? That is the question 
				also, have we saved ourselves from the turmoils and difficulties 
				of the economy? <br>
				Tim: And you would say no?<br>
				Musab: I would say no (emphatic).<br>
				Tim: Marcos Belucco<br>
				Marcos: I think we have a good example of diversification for 
				the future of Oman. A huge investment is going to generate a lot 
				of opportunities. The government is building ports, airports, 
				railways and a lot of opportunities would be created for the 
				Omanis.<br>
				<br>
				Tim: Come back to the suggestion that large companies are only 
				serving their own interests rather than the interests of the 
				country.<br>
				<br>
				Marcos: Yes, the price of the gas is attractive but in order to 
				receive this gas, you have to contribute to the society. You 
				have two ways to generate opportunities in our companies. The 
				first is to recruit the locals, train and qualify them to work 
				in our companies.<br>
				<br>
				The second way to provide opportunities is to develop the local 
				suppliers to work with the companies and to have clear evidence 
				that we are adding value to the community.<br>
				<br>
				<b>Tim: But you are not here for social service, you are here to 
				make money aren’t you?<br>
				</b>Marcos: Yes, you have to balance this. You have to find a 
				balance between your company, your social responsibilities and 
				job creation. You have to find a balance between all these 
				factors because social responsibility is part of our business.
				<br>
				<br>
				Tim: Ok question from the front row.<br>
				Tawfeeq Al Lawati: My vote was NO. I don’t think these mega 
				projects (I should not generalise them, but the ones I am 
				talking about are those invested by the government) are not for 
				the benefit of the local economy. <br>
				<br>
				Tim: They are not giving you what you want…<br>
				Tawfeeq: Initially, the plan was to diversify the income and the 
				expectation was to achieve 35 per cent income from industrial 
				sector out of the 60 per cent non-oil revenues. That is value 
				additions to local natural resources – creating job 
				opportunities and basically returns on the investment. Today – 
				and we have reviewed the budget for 2013 – most of these 
				projects are not contributing revenues for the budget. In fact 
				some of them are loss-making and taking good advantage of the 
				cheap gas. <br>
				<br>
				Tim: So they are working for themselves, not for the country.<br>
				Tawfeeq: They are working. Unfortunately, some companies have 50 
				per cent partnership and they are getting advantage of the cheap 
