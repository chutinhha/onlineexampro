						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Keeping the menace at bay</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						The annual ‘Beat Diabetes’ campaign by Landmark Group 
						seeks to generate wider awareness in the society about 
						the growing diabetes epidemic across the Sultanate. 
						Muhammed Nafie reports<br>
                		<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
                		Diabetes is increasing at an alarming rate across the 
				world. Over 300 million people worldwide are living with 
				Diabetes, and the number of people living with diabetes is 
				predicted to reach 430 million by 2025. Oman continues to be one 
				of the ten top countries with the highest prevalence of diabetes 
				in the World, as the country was ranked sixth in 2007 and ninth 
				in 2010 by the International Diabetes Federation (IDF), an 
				umbrella organisation of over 200 national diabetes associations 
				in over 160 countries.<br>
				<br>
				Awareness is the first step in the battle against diabetes. 
				Keeping this in mind, Landmark Group, the hospitality and retail 
				conglomerate adopted the fight against diabetes as part of its 
				CSR initiative in 2009 in the UAE, launching the ‘Beat Diabetes’ 
				initiative in order to make people alert about the growing 
				diabetes epidemic across the world. <br>
				<br>
				The campaign has extended into five new countries in the 
				following years such as Oman, Kuwait, Qatar, Bahrain, Saudi 
				Arabia and India. Walkathons were held across all six countries 
				with over 25,000 people participating. The Group also conducted 
				free blood glucose tests for over 30,000 people. Landmark 
				Group’s campaign will assist people in creating consciousness 
				about the long term disorder. In 2011, the group partnered with 
				the International Diabetes Federation (IDF), becoming the 
				organisation’s first retail corporate supporter partner. This 
				relation has also made Landmark one of the official partners of 
				the World Diabetes Day.<br>
				<br>
				</font>
						<img border="0" src="images/health-nov-12-3.jpg" width="250" height="227" align="right" hspace="8" vspace="3"><font face="Verdana" size="1"><b>Mitigating 
				risks<br>
				</b>As part of the campaign in Oman in 2012, the group is 
				hosting blood-sugar testing camps across various regions in the 
				sultanate to create awareness on diabetes. In order to mitigate 
				risks caused by late diagnosis of the condition, diabetes 
				testing centres will be set up at multiple locations across the 
				Group’s retail network to administer free blood glucose test for 
				the community leading up to the annual walkathon during the 
				Diabetes awareness month of November. The first awareness 
				campaign was held at Centrepoint – Salalah on October 26 and 27 
				followed by campaigns in Sohar, Muscat, and corporate offices in 
				the country. <br>
				<br>
				Several private and public organisations such as Muscat Pharmacy 
				and Al Raffah Hospital have extended their support towards this 
				initiative and would be assisting in conducting test at all the 
				camps. Seeniya Biju, COO of Al Raffah Hospital, one of the 
				partners in the campaign, says, “We are indeed happy to be part 
				of the Landmark Group’s CSR initiative, Beat Diabetes campaign 
				for the second year. Diabetes is a growing dreadful disease that 
				leads to death and requires more awareness all across the 
				world.” She adds that being one of the largest campaigns in Oman 
				it could cover all parts of the country and spread the message 
				to a cross section of people. The campaign has also been very 
				effective in diagnosing some cases at an early stage. It shows 
				how effective is has become in curbing the menace.<br>
				<br>
				</font>
						<img border="0" src="images/health-nov-12-2.jpg" width="250" height="197" align="right" hspace="8" vspace="3"><font face="Verdana" size="1">Dr 
				Saida Noor Al Busaidi, senior consultant, Royal Hospital, and 
				Chairperson of National Diabetes Management Service Development 
				Committee corroborates, “ ‘Beat Diabetes’ campaign is very 
				important to raise awareness at all levels and sectors, both for 
				the healthy and those affected with diabetes- for the prevention 
				and control of the disease and prevention of complication.” She 
				urges everyone to understand that diabetes is not a mild 
				disease, but a complex chronic disease which has lots of 
				consequences if the patient does not control it.<br>
				<br>
				Dr Saida also highlights the importance of joint efforts by 
				government and private sector to promote a healthy lifestyle 
				focusing on women and children and providing the means for 
				people to live a healthy lifestyle.<br>
				<br>
				Referring to some of the alarming numbers she added, “The GCC 
				countries are among the top 10 in the world. In Oman we have 
				137,000 adult diabetes patients excluding children with diabetes 
				and pregnant women with diabetes. Obesity and diabetes go hand 
				in hand and in Oman. Overweight and obese represent 60 per cent 
				of our population. As many as 35 per cent people are in a pre 
				diabetes stage and people these will become diabetic if they do 
				not practise a healthy lifestyle.”<br>
				<br>
				Huwayda Nasser Al Shahimi, Owner of FitBody Diet and Fitness 
				Centre, says being overweight or obese will increase one’s risk 
				of complications. “Losing just a few kilos through exercise and 
				eating well can help with diabetes control and reduce the risk 
				of other health problems,” she said. “Just 30 minutes a day of 
				moderate physical activity reduces the occurrence of developing 
				diabetes. You will also have more energy and feel better in 
				general.” Huwayda also informs that FitBody gives training on 
				how to lose weight in a healthy way and keep off fatty foods.”
				<br>
				<br>
				The national health surveys over the past three decades have 
				shown increasing prevalence of diabetes mellitus in Oman. The 
				first National Health Survey on diabetes, which was conducted in 
				1991, showed a prevalence of 9.75 per cent, while the National 
				Health Survey in the year 2000 showed that the prevalence has 
				increased to 11.6 per cent among adults over 20 years of age. In 
				addition, the NHS (2000) also demonstrated high prevalence of 
				hypertension, dyslipidemia and overweight and obesity combined. 
				These were 35 per cent, 40 per cent and 48 per cent 
				respectively. Tobacco use among adults was also prevalent at 23 
				per cent. A recent National Health Survey (2008) has shown that 
				diabetes has increased significantly to 12.3 per cent, along 
				with an increase in risk factors such as obesity and physical 
				inactivity. Currently, the total number of Omanis living with 
				diabetes and registered at the ministry of health surpasses 
				170,000 and this is increasing 5,000 annually.<br>
				<br>
				<b>Prevention efforts<br>
				</b>According to Dr Sulaiman Al-Shereqi, Priority Diseases 
				Control Section, Ministry of Health and senior specialist in 
				Public Health, and who is supervising the National 
				Non-communicable Diseases Screening Programme, “The public 
				health assessment of the current situation leads to the 
				conclusion that the Ministry of Health must give equal attention 
				towards developing the quality of both the curative services as 
				well as the preventive services. This requires the cooperation 
				of all health stakeholders in redefining the public health 
				priorities especially those linked to healthy lifestyle and 
				healthy living. Success in implementing preventive health 
				strategies would start to show effects in a decade’s time. 
				Therefore, it is essential to start building collaborations and 
				work together now. The partnership with Landmark Group in their 
				initiative ‘fight against diabetes’ for the third year in a row 
				presents a good example of ‘private public partnership’ which 
				the World Health Organisation is recommending in the fight 
				against the non-communicable disease epidemic.”<br>
				<br>
				Towards instituting public health measures to control 
				non-communicable diseases, Oman has implemented a national 
				non-communicable diseases screening programme since 2007. The 
				programme is currently provides a screening service for all 
				Omanis aged 40 years and above. The screening targets five 
				common conditions, which include diabetes, hypertension, chronic 
				renal impairment, obesity, and hypercholesterolemia. The main 
				objectives of this programme is:<br>
				Early detection of non-communicable diseases (NCD) to initiate 
				early treatment and delay or prevent complications<br>
				Early detection and intervention of pre-NCD to delay or prevent 
				NCD<br>
				Early detection and intervention of NCD risk factors e.g.; 
				obesity, smoking, alcohol use, physical inactivity, overuse of 
				nephrotoxic drugs and unhealthy diet<br>
				To educate and promote good health<br>
				To increase the awareness about the burden of NCD and the role 
				of early screening<br>
				Muscat Pharmacy along with its Principal Lifescan – J&amp;J Company 
				have teamed up with the Landmark Group to provide free screening 
				of participants and attendees at various locations during the 
				‘Beat Diabetes Campaign’ over the last couple of years. The 
				Walkathon programme of 2011, involved screening of nearly 3,000 
				attendees. <br>
				<br>
				Says Pradeep Shetty, sales manager, Muscat Pharmacy, “Screening 
				helps in identifying persons with diabetes (undiagnosed cases), 
				in existing diabetes patients, the screening process helps in 
				identifying whether their blood glucose is under control. It 
				helps in increasing awareness and educating both the patient and 
				caregivers or family members.” While a lot is being done to 
				combat the growing incidence of Diabetes, more needs to be done 
				in this field.&#8194;<br>
