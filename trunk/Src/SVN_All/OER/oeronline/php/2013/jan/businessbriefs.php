				participation from a new strategic investor Royal Dutch Shell 
				group as well as leading energy investors RockPort Capital, Nth 
				Power and Chrysalix Energy Venture Capital, an existing 
				investor. The funding is a key step to future large-scale 
				deployment of GlassPoint’s solar steam technology in EOR 
