				parabolic troughs inside a glasshouse structure, which protects 
				the system from high winds and seals the solar collectors from 
				dust, dirt, sand and humidity. The protected environment enables 
				the use of ultralight, low-cost reflective materials and 
				automated washing equipment, further reducing costs and water 
				use. Furthermore, GlassPoint steam generators are designed to 
				use the same low-quality boiler water as once-through steam 
				generators, the industry’s current best practices, eliminating 
				the need for costly water pretreatment. <br>
