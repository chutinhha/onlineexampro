				&nbsp;<p align="justify" class="style13">
				As part of its corporate social responsibility programme and 
				commitment to serve the society in areas where it operates from, 
				Jotun Paints has extended support to Dar al Hanan, a centre 
				dedicated to children undergoing cancer treatment. Managed by 
				National Association for Cancer Awareness (NACA) and located in 
				South Ghubra, near Royal Hospital, Dar al Hanan is Oman’s first 
				home-away-from-home for children afflicted with cancer.<br>
				<br>
				The support from Jotun Paints to NACA is the continuation of the 
				cooperation they started in 2011. The contribution in 2012 came 
				from Jotun employees, loyal and supportive dealers and from the 
				company itself.<br>
				<br>
				Dar al Hanan addresses a critical need of parents accompanying 
				children to Royal Hospital for cancer treatment by providing 
				them with family accommodation. A total of 16 residential units 
				are available to offer this facility.The centre provides 
				accommodation during treatment and ensures patients follow 
				treatment (chemotherapy and radiotherapy) on a regular basis. 
				Volunteers at the centre provide moral support and help with 
				administrative procedures related to healthcare and counselling 
				to overcome the trauma of the critical illness.<br>
				<br>
				‘The contribution of Jotun Paints is a continuation of the 
				cooperation we started with NACA in 2011. We donated RO5000 in 
				2011. In 2012, in cooperation with eight Jotun dealers we have 
				been able to collect another RO5000 that was handed over to Dar 
				al Hanan Centre. The Centre has been established based on the 
				need to provide valuable accommodation for the kids who have 
				families accompanying for their cancer follow up at the Royal 
				Hospital in Muscat,” said Andreas Stolt-Nielsen, sales and 
				marketing manager in Jotun.<br>
				<br>
				On his part, Bader al Maqdasi, assistant marketing manager said 
				that the contribution from the company this time was different 
				as the senior management was engaged with children in a number 
				of activities such as drawing to help children erupt their 
				talents and express their emotions. “Doctor Waheed has 
				introduced us to the association’s mission and also thanked 
				Jotun for our support. The senior management of Jotun toured the 
				building and spent time with the children at the centre.”<br>
				<br>
				He added, “We are looking forward to be engaged in more 
				activities in the future as such activities will bring smiles to 
				these faces. They also provide us with valuable opportunities to 
				pay back to the society we are working in and translate our 
				social responsibility values into reality.” He expressed 
				confidence that other corporates would do the same and provide 
				such non-profit organisations with their utmost support.”&#8194;</td>
