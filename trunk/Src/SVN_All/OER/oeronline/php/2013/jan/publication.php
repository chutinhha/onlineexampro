						<font face="Verdana" size="1">Wayne Pearce, CEO, Oman 
						Air, has announced that the publications of United Media 
						Services (UMS), the largest media establishment in Oman, 
						will now be available on board Oman Air, the national 
						carrier of the Sultanate of Oman. Oman Air passengers 
						travelling on long-haul Airbus A330s can now read the 
						digital editions of all the business, lifestyle and 
						luxury magazines of UMS, both in Arabic and English on 
						the aircraft’s In-Flight Entertainment System. The UMS 
						magazines displayed on board the Oman Air flights 
						include Oman Economic Review (OER), and Alam al-Iktisaad 
						Wal A’mal (AIWA), Oman’s premium business magazines in 
						English and Arabic respectively; The Woman, Al Mar’a, 
						and Signature.<br>
						<br>
						“We are very pleased to have UMS publications displayed 
						on Oman Air’s In-Flight Entertainment System,” says 
						Wayne Pearce. “This partnership will further enhance 
						quality reading material for our passengers. In 
						addition, many people who may not be able to read the 
						print edition fully will now have the opportunity to 
						enjoy the magazine from start to finish while flying 
						Oman Air.”<br>
						<br>
						“Our partnership with Oman Air has placed us among the 
						select band of publishing companies across the world 
						that have integrated their publications in digital form 
						with an air carrier’s in-flight entertainment system. It 
						is a proud moment for us. The development is quite 
						significant from the discerning readers’ perspective as 
						they are increasingly consuming the content on digital 
						platforms, more so when they are flying,” says Sandeep 
						Sehgal, chief executive, United Media Services.<br>
						<br>
						“This partnership brings us an outstanding opportunity 
						to enhance the readership of our magazines manifold,” 
						says Alpana Roy, executive vice president of United 
						Media Services. “As a media establishment, we invest our 
						resources to create quality content which will now no 
						longer be restricted to our print readers but will reach 
						out to a wider section of online readers. This combined 
						strength of print and <br>
						<br>
						digital media will benefit our readers in many ways. Our 
						advertisers will also enjoy an enhanced exposure to this 
						audience and will be able to leverage the opportunities 
