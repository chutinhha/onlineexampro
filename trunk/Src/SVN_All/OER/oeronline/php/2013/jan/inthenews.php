						Aston Martin partners Investindustrial</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Investindustrial will become a major shareholder in 
						Aston Martin by investing £150mn for a 37.5 per cent 
						stake in the company</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-jan-13-1.jpg" width="400" height="230"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Aston Martin has announced its partnership with leading European 
				investment firm Investindustrial which will invest £150mn in the 
				form of a capital increase for a 37.5 per cent stake in the 
				company. Aston Martin has welcomed Investindustrial as a major 
				shareholder and confirmed that it would now proceed with its 
				extensive and exciting plans for sustainable long-term growth.<br>
				<br>
				Aston Martin chairman David Richards said, “I am delighted that 
				Investindustrial has decided to become a major investor in Aston 
				Martin. With the support of The Investment Dar, we have made 
				substantial progress over the past five years in laying the 
				foundations for success as one of the world’s leading luxury 
				sports car manufacturers. Investindustrial’s new investment 
				reflects and sustains the unique position of Aston Martin within 
				the industry. With this partnership and the continued commitment 
				of The Investment Dar, we look forward to working with our 
				shareholders as we realise our vision and exciting future 
				plans.”<br>
				<br>
				Andrea C Bonomi, senior principal at Investindustrial, comments: 
				“We are delighted to form part of this iconic global, but 
				quintessentially British brand. We are looking forward to 
				working with the management and Investment Dar to achieve a 
				similar transformation and rejuvenation that we achieved with 
				Ducati, by expanding the model range and strengthening the 
				dealership network, throughout the world.”<br>
				<br>
				Adnan Al-Musallam, chairman &amp; managing director of The 
				Investment Dar said: “We welcome Investindustrial as new 
				partners in our collaboration with Aston Martin. With our 
				continued commitment and the support of Investindustrial, Aston 
				Martin is in a strong position to pursue its plans for 
				development.”<br>
				<br>
				Aston Martin intends to invest more than half a billion pounds 
				in its new product and technology programme over the next five 
				years. With the support of The Investment Dar and 
				Investindustrial, the company is well positioned to realise its 
				ambitious growth strategy.<br>
				<br>
				Aston Martin production will remain at the luxury British 
				marque’s global headquarters at Gaydon in Warwickshire, a 
				purpose-built facility where a skilled workforce of 1600 
				employees assemble the current range of sports cars.&#8194;<br>
