						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Is Oman Future Ready?</font></b><p class="style15">
				<span class="style14"><strong>Oman Debate 2012 sets a roadmap 
				for the Sultanate’s socio-economic development in the coming 
				years. Excerpts from Session 1, titled ‘Is Oman Future Ready?’ 
				which threw fresh insights into shaping a better tomorrow for 
				the country</strong></span><p align="center" class="style13" dir="ltr">
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-jan-13-2.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="400" height="210" hspace="7"></font><p align="justify" class="style13" dir="ltr">
				Tim Sebastian: Before I speak to the panellists, I would like to 
				get the views of the audience. <br>
				<br>
				<b>How far is Oman future ready and what should the priorities 
				be?</b><br>
				Audience (Wael Lawati): Creating jobs for the youth is a 
				priority as 70 per cent of our population is under the age of 
				40. It should start from an early age and we need not wait till 
				the University level. There is a need to recreate the natural 
				spirit of entrepreneurship of our forefathers. Omanis have lost 
				that spirit of entrepreneurship, due to our cushy jobs. The 
				government’s plans to promote SME development need to be 
				strengthened and there needs to be a feeling that one can create 
				one’s own destiny instead of just queuing up for a government 
				job.<br>
				<br>
				<b>Tim: Sir, how business friendly is Oman?<br>
				</b>Audience: In many ways it is, but it depends on what we 
				compare it to. Compared with our regional peers, we are doing 
				fine, but there are challenges and a lot needs to be done. 
				Creating employment and training the young workforce and keeping 
				up the pace of development in the face of depleting resources 
				should be our priorities. <br>
				<br>
				<b>Tim: How far are a burgeoning public sector and a declining 
				private sector a cause of concern for you?</b><br>
				Audience: There are definitely new ways of collaboration, but 
				certainly it is a big worry at the moment. The overwhelming 
				dependency of the economy on the petrochemical sector is a cause 
				for concern in the face of depleting oil and gas resources and 
				there is a need to diversify the base of the economy, probably 
				towards the financial sector. <br>
				<br>
				Audience: The Omani workforce should be given practical training 
				and the standard of education from primary education till the 
				university level needs to improve. In the last three years, 
				things are moving in the right direction, but the pace of change 
				needs to quicken. <br>
				<br>
				<b>Tim: Manal Al-Abdwani, you heard some of the concerns of the 
				audience: jobs, too much red tape and perhaps not being business 
				friendly enough compared to other countries in the Gulf. How 
				would you react to that? </b><br>
				Manal Al-Abdwani: I agree with the audience on some of these 
				issues. If we talk about job creation, this is definitely 
				priority No 1 for Oman right now. But let’s put things in 
				perspective. If you look at the data coming from IMF and local 
				sources, we find that on an average 40,000 to 45,000 jobs need 
				to be created every year. Is this difficult to achieve? Let’s 
				look at where they can be employed. If we look at the success 
				stories so far it has been in the banking sector, oil and gas, 
				health and education; so Omanis are looking for certain types of 
				jobs, which offer growth prospects, good remuneration etc. <br>
				<br>
				Tim: Why do a larger number of expatriate workers come into the 
				country? At last count over 100,000 new expat workers have come 
				into the country last year.<br>
				<br>
				Manal: This is because we have a lot of infrastructure projects 
				going on and a lot of development around construction and 
				manufacturing. The nature of the economy does not sustain the 
				inclusion of Omanis based on the aspirations they have. So there 
				is a need to see how we can include more Omanis in the type of 
				economy that is being developed. So we need to see how we can 
				include more Omanis in the type of economy that we want to 
				develop. <br>
				<br>
				<b>Tim: This has been an issue for some time now or have you 
				come into it now?<br>
				</b>Manal: No, we have not come into it now; what we are seeing 
				is that there is a need to act now. Now that we know the number 
				of Omanis who need to be employed every year, let’s look at 
				where they can be included. For example the construction sector 
				has been able to employ only 60,000 Omanis, while there are 
				600,000 expatriates in that sector, so we have been able to 
				employ only 10 per cent of the labour force. Compared with this, 
				the retail sector seems more appealing as there are 40,000 
				Omanis in that sector.<br>
				<br>
				Tim: HE Tawfeeq al Lawati, I am sure this is an urgent issue 
				now, but I would like to present it in a different way. Forty 
				years ago, when we started the Renaissance, we did not have 
				enough skilled and qualified people, so bringing in expatriates 
				was a necessity and a must, but after 40 years we need to change 
				the formula.<br>
				<br>
				<b>Tim: So why is this still going on?<br>
				</b>HE Tawfeeq: This is the point that I would like to raise. It 
				should not sound like that we should try to include Omanis. The 
				message to everyone should be that expatriates are not an option 
				and we have to survive with Omanis; and with this in view we 
				should put forward what is required from the government and what 
				is required from the private sector. <br>
				<br>
				Tim: There are some concerns vis-à-vis their education and 
				whether Omanis are in a position to take on the jobs of the 
				expatriates?<br>
				HE Tawfeeq: If the education is not efficient, then we need to 
				revise the educational process. Right now this is not up to the 
				mark; there are a lot of complaints about the standard of basic 
				education and higher education. HE the Vice Chancellor of the 
				Sultan Qaboos University, which takes the cream of the students, 
				came to the Majlis A’Shura and said that students required a 
				two-year foundation course before they can join the University.
				<br>
				<br>
				<b>Tim: This is still the case?<br>
				</b>HE Tawfeeq: Yes this is still the case. We have a genuine 
				problem and we have to identify where the problem is. Today we 
				spend almost RO1bn on education which is almost 8-10 per cent of 
				the yearly budget, but we are not getting the qualitative 
				outcome.<br>
				<br>
				<b>Tim: You have the director general of planning and follow-up 
				here. What according to you should be the priority No 1? </b>
				<br>
				HE Tawfeeq: The priority No 1 for me should be Omanis; we should 
				focus on them and all our plans should focus on serving Omanis, 
				whether it is in education, health services or any other service 
				that the government provides. <br>
				<br>
				<b>Tim: What I am trying to get to is that, what is the 
				government not doing and what should it do? </b><br>
				HE Tawfeeq: Today, we have a higher committee for economic 
				planning, and we would like to listen to them in terms of what 
				they are proposing. What we have today in education is oriented 
				towards individuals. When a new minister comes, there is a lot 
				of change in the manpower and the leaders, but there is no 
				genuine change in the competency or skills that the students are 
				expected to master to join the workforce.<br>
				<br>
				<b>Tim: Abdulaziz al Baluhsi, what are your major concerns?<br>
				</b>AbdulAziz: The first question that we need to ask is whether 
				unemployment is a big issue or it is something that we ourselves 
				create. <br>
				<br>
				Tim: Is it a big issue? Probably at 24 per cent unemployment is 
				a big issue. <br>
				<br>
				AbdulAziz: Is it really 24 per cent? And this is a question that 
				we need to ask. <br>
				<br>
				<b>Tim: What in your opinion is the right unemployment figure?<br>
				</b><br>
				<b>AbdulAziz: If the figure is correct then we need to ask who 
				the unemployed are- are they University graduates or only the 
				people who do not want to work?<br>
				</b><br>
				<b>Tim: Or is it people waiting for lucrative government jobs, 
				rather than taking lower paid jobs in the private sector?</b><br>
				AbdulAziz: Yes. So we need to analyse the data and see where the 
				problem lies rather than just saying that this is the big 
				problem. The major problem is that today we are talking about 
				our challenges without any in-depth analyses of the problems. 
				The government and private sector have to work together to go 
				deeper and find the roots of the problem. <br>
				<br>
				<b>Tim: It sounds fine in theory but what does it mean in 
				concrete terms? </b><br>
				AbdulAziz: In theory it is fine, but in practical terms if all 
				of us have an open mind things would work better.<br>
				<br>
				Tim: Nobody has said that you should not work together.<br>
				<br>
				AbdulAziz: We could work closer and better. <br>
				<br>
				<b>Tim: What are the concrete steps or results that we should be 
				taking about in a year’s time?<br>
				</b>AbdulAziz: Look at the banking sector. In the sector 90 per 
				cent of the workforce comprises Omanis: you have successful CEOs 
				and heads of IT and compliance who are all Omanis. So let’s look 
				at how Omanis have been so successful in the banking sector and 
				replicate that model in other sectors. So we can work together 
				and there is a model that is working. <br>
				<br>
				<b>Tim: If this has worked in the banking sector, then why have 
				the other sectors not done the same?</b><br>
				AbdulAziz: That’s a question that we have to ask.<br>
				<br>
				<b>Tim: What’s the answer, because we are not just asking 
				question but are looking for answers?</b><br>
				Manal: On the point of why it is working in the banking sector: 
				it works because Oman is one of the first countries to create a 
				good training institute for the banking sector (College for 
				Banking and Financial Services) and that has created the 
				workforce needed in this sector.<br>
				<br>
				<b>Tim: Is this model replicable in other sectors?<br>
				</b>Manal: Let’s go through it. The banking sector remunerates 
				well; it has good growth prospects and provides the kind of job 
				that Omanis aspire for. There are certain sectors that seem to 
				be successful in attracting Omanis- banking is one; oil and gas, 
				education and health are catching up. <br>
				<br>
				<b>Tim: The oil and gas sector has seen a lot of unrest this 
				year, sit-ins and strikes?<br>
				</b>Manal: We are expecting 8000 jobs to be created for Omanis 
				in the oil and gas sector this year, so we need to focus on 
				those sectors which match the aspirations of Omanis. The other 
				area that we need to focus on is our successful businesses today 
				– whether they are the family businesses or the bigger 
				industries, or government industries and businesses. The 
				government and companies should look at the nature of their 
				businesses, see the value chain and the kind of opportunities 
				that can be created within these companies. His Excellency spoke 
				about outsourcing and the possibilities of subcontracting and 
				partnerships in his keynote address, so there is a lot that can 
				be done by these companies as initiators of&nbsp; small 
				enterprises. <br>
				<br>
				<b>Tim: We will speak about this in detail in a while. Stephen 
				Thomas, what keeps you awake at night regarding the future of 
				Oman? </b><br>
				Stephen: The subjects that my colleagues on the panel have 
				already touched upon- it is about employment and creating jobs 
				for Omanis. <br>
				<br>
				<b>Tim: You have spoken about a human resources nightmare?</b><br>
				Stephen: I am not saying that it is a nightmare but there are 
				issues. The future economic prosperity of Oman depends on how 
				many Omanis are in the workforce and that is a problem for all 
				of us as it also depends on how productive they are in that 
				work. And given the examples that Manal has given it does show 
				that it is possible to have those success stories and there are 
				Omanis working successfully at every level of the economy. <br>
				<br>
				Tim: Omanis make up only 40 per cent of the private sector, 
				which is expected to generate the real wealth of this nation!<br>
				<br>
				Stephen: The private sector can step up its efforts in 
				generating more employment for Omanis in some areas, where they 
				have not been successful in developing an Omani workforce, but 
				there are other issues as well. The burgeoning public sector 
				that you mentioned in the question at the beginning of the 
				debate is a challenge for the private sector. We want less 
				government and less red tape, not a growing government in order 
				to create wealth, because that’s the reason why the private 
				sector is there. And it has not helped the private sector in 
				various sectors to develop their own Omani workforce because 
				there has been an outflow of people from the private sector to 
				government jobs that have been created in the last year and as 
				His Majesty has said the creation of jobs in the public sector 
				is not sustainable. So it is about job creation. Manal has given 
				the example of the oil and gas sector, which I am aware of. Even 
				if we manage to Omanise every single job in that sector there 
				are 25,000 people working directly and 40,000 people working 
				outside the fence. It is not going to solve the next year’s 
				intake problem. <br>
				<br>
				<b>Tim: How do you react to Tawfeeq al Lawati’s statement saying 
				that every job in the country should be Omanised?</b><br>
				Stephen: I think His Excellency’s comment is an idea for 
				everyone. Private sector would want to have a local workforce as 
				a most efficient way of operating any business anywhere in the 
				world. <br>
				<br>
				<b>Tim: You are on record stating expatriates are not a 
				commodity to be Omanised?</b><br>
				Stephen: I don’t believe they are. <br>
				<br>
				<b>Tim: Do you think you are being used that way?</b><br>
				Stephen: I do believe that we should look at expatriates who are 
				here; and they should be here because they are in a job for 
				which there is not a skilled equivalent in terms of an Omani.<br>
				<br>
				<b>Tim: The comment suggests that you feel undervalued here; do 
				expatriates feel undervalued here?</b><br>
				Stephen: It is possible, if you get a general statement like 
				expatriates are not an option anymore and it should only be 
				Omanis going forward. That’s well and good if there are Omanis 
				who are future ready, with the human resources.<br>
				<br>
				<b>Tim: And are they ready?</b><br>
				Stephen: No, they are not. There is more that the private sector 
				should do. His Excellency the Minister spoke about every sector 
				getting its own training institutes, so there is a lot that the 
				private sector can do. But at the moment we are not future 
				ready. <br>
				<br>
				<b>Tim: let me take up a question coming on Twitter and let me 
				pose it to Ahmed al Wahaibi- Are Omanis qualified to take on new 
				jobs?</b><br>
				Ahmed: In short, yes.<br>
				<br>
				<b>Tim: Despite the reservations that HE Tawfeeq raised in terms 
				of education?</b><br>
				Ahmed: Let me first comment a bit about Omanisation. Omanisation 
				has come to acquire a negative connotation. As locals, we are 
				entitled to a job. If someone gives to an Omani a job it is seen 
				as a favour. This perception should change as Omanis have a 
				right to get a job. Preparing an Omani for a job is a natural 
				thing to do and it is good for the economy. Secondly it is good 
				to see more expatriates in the country as we are a growing 
				economy.<br>
				<br>
				<b>Tim: So you think having more expatriates is a good sign?</b><br>
				Ahmed: Yes, I think. If we get our policies right ,then if we 
				have more expatriates, it shows that there is trust in the 
				country. <br>
				<br>
				Tim: Come back to the issue of whether Omanis are qualified.<br>
				<br>
				Ahmed: Yes they are. In Oman Oil Company we have 7,000 people 
				out of which 74 per cent are local and they are in highly 
				technical jobs and they include business leaders and CEOs. <br>
				<br>
				<b>Tim: Tawfeeq al Lawati, would you agree with this view point?</b><br>
				HE Tawfeeq: My point is that the problem is with the attitude. 
				The attitude is since we have access to expatriates, why we 
				should prepare Omanis, train them and do the hard work. This is 
				the attitude of the private sector. The private sector needs to 
				be informed that, “We are now in 2012 and you give us your 
				business plan for the next eight years detailing how you are 
				going to Omanise your workforce.” I am not saying we have to do 
				it overnight, we are not equipped to do it overnight. But over 
				the next eight years you ask me what resources you need for the 
				purpose. We are investing millions of rials in infrastructure, 
				highways and ports. The most important and valuable asset for 
				the country is the Omani citizen and we cannot have all this 
				while still depending on expats. We had a crisis during the Gulf 
				War when a number of expatriates went out to work as they were 
				paid higher or when they moved out because of the political 
				risks. We have to depend on our people for our future.<br>
				<br>
				<b>Tim: Let me put the same question to you – Are Omanis 
				qualified to take on new jobs?</b><br>
				HE Tawfeeq: No, at this point and this is our challenge. We need 
				to have a plan for the next 8-10 years. The government has to 
				invest in partnership with the private sector. They have to 
				identify each organisation and what are the positions available. 
				I don’t want Omanis to be working only for low-paid jobs, we 
				have to tackle, mid-management and top-management jobs. <br>
				<br>
				<b>Tim: Another question coming up on Twitter – Where does the 
				capacity building plan stand at the end of 2012, ready for 
				higher levels of Omanisation? </b><br>
				HE Tawfeeq: Omanis are not available totally, but we need to 
				prepare them. The private sector is not taking this initiative 
				today. There are companies which are dependent on government 
				subsidies in terms of gas and other subsidised services. If we 
				recruit management trainees and invest in them, even government 
				companies like Mr Ahmed al Wahaibi’s company should work as 
				hatcheries from whom the private sector can recruit Omani 
				leaders. Even banks hire only for their needs; it is all about 
				balance sheet, profit and loss. Companies from the public and 
				private sectors should be socially responsible; they should 
				recruit more Omanis and train them.<br>
				<br>
				Tim: Do you think that Oman’s planning is coherent; you have 
				gone on record saying that Oman had a plan called 2020, but 
				after five or six years it disappeared and it needs revision.<br>
				HE Tawfeeq: Vision 2020 is still mentioned but it is irrelevant 
				today. Vision 2020 was drawn up when oil prices were $17 per 
				barrel, while oil prices today are at $107 per barrel. <br>
				<br>
				<b>Tim: Does the plan exist today?<br>
				</b>HE Tawfeeq: The plan exists but it is not relevant; it 
				should have been revised based on the facts. <br>
				<br>
				<b>Tim: Manal, do you want to respond to that? Do you think that 
				the plan should have been revised?</b><br>
				Manal: I totally agree and there are a number of steps being 
				taken in this direction. The Higher Council of Planning is 
				taking great efforts...<br>
				<br>
				<b>Tim: So when are we going to see a new plan?</b><br>
				Manal: It is going to be available very soon and a lot of 
				discussions are going on around it.<br>
				<br>
				<b>Tim: Weeks, days, months?<br>
				</b>Manal: His Excellency the Minister is here and you could ask 
				him.<br>
				<br>
				Tim: I would actually like to ask His Excellency Ali Al Sunaidi 
				the Minister of Commerce and Industry as to when the new plan is 
				expected. <br>
				HE Ali Al Sunaidi: Thank you for inviting me to the debate. <br>
				<br>
				Tim: (Laughs) We wouldn’t have it any other way.<br>
				HE Ali Al Sunaidi: First of all thank you, you are doing a 
				brilliant job and I am very fascinated by the spirit of the 
				debate. Let me say that Vision 2020 still survives because the 
				ports that are under construction are in that plan. The 
				concessions that the government gave are a part of 2020. <br>
				<br>
				Tim: It’s a bit worrying, if the Majlis A’Shura does not know 
				that the plan is still relevant.<br>
				HE Ali Al Sunaidi: He did not say that (alluding to HE Tawfeeq), 
				he said that it is out of sync, and I agree with that, because 
				the plan at that time was all about hardware or infrastructure. 
				Starting from this year as we prepare for the five-year plan and 
				I agree that during the course of this five- year plan the 
				government should announce a new vision; that’s already on 
				board, it’s been discussed at various levels. <br>
				<br>
				<b>(Audience applause)<br>
				Tim: Is there a time table for it?</b><br>
				HE Ali Al Sunaidi: I would not comment on it, as it is not 
				appropriate to do so here. Thank you. <br>
				<br>
				<b>Tim: Let’s take the first vote of the day and let’s make it 
