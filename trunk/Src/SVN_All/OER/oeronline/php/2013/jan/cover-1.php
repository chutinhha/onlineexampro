				come to us and deal with us do so in US dollars. I can 
				sympathise with the people who are in the money business and the 
				people who are buying equipment that is not priced in US 
				dollars. This was not just debated here but also in other 
				countries like Saudi Arabia, Qatar etc. I agree that we should 
				have been more frank and communicated with our people about the 
				pros and cons of the dollar peg.<br>
				<br>
				<b>Tim: So you agree that you need to communicate more?</b><br>
				HE Sunaidi: Yes I do agree that the government needs to 
				communicate more.<br>
				<br>
				<b>Tim: And should you also tell the Majlis A’Shura as to what 
				they are voting for?</b><br>
				(Audience laughter)<br>
				HE Sunaidi: That’s an issue that the Majlis has discussed with 
				the Minister of Finance, when he visited the Shura Council and 
				spent seven to eight hours there.<br>
				<br>
				Tim: It does not seem to have been resolved.<br>
				<br>
				HE Sunaidi: I think they need to visit his office again. <br>
				<br>
				Some people need to go back to the background while discussing 
				an issue like this which needs to be discussed with bankers and 
				economists. One should not talk in isolation. There should be 
				more such debates in Oman’s newspapers and you see such 
				discussions happening in the newspapers. This issue calls for 
				more transparency. <br>
				<br>
				<b>Tim: There is a related question on twitter asking – “Will 
				the private sector be able to partner better with the government 
				if the government was more transparent?</b><br>
				HE Sunaidi: Yes, they would and I would like to say that 
				transparency should be the main issue of the next few years to 
				come.<br>
				<br>
				<b>Tim: So Mr Minister, will you come back and tell us in a 
				year’s time as to how transparent you have been? </b><br>
				HE Sunaidi: The world is watching and Oman is certainly not the 
				worst country in the world; it is amongst the top 10 in many 
				things.<br>
				<br>
				<b>Tim: But you want it to be the best country and not amongst 
				the worst?</b><br>
				HE Sunaidi: I understand that, but it is unfair to say that 
				since you are No 5 or No 10 you are bad. One needs to understand 
				as to how come we are No 10 right now. I would conclude that 
				this debate which we should encourage shows us that we should be 
				more transparent, frank but also more active in various areas 
				and people who have been here know that this is happening.<br>
				<br>
				Tim: And I am sure your colleague the Minister of Information 
				shares your views.<br>
				HE Sunaidi: He is not just sharing this view but is doing that 
				already.<br>
				(Audience applause)<br>
				Audience question: My name is Nutayla and I work for PDO. If I 
				look at the textbooks in schools today we find that they are the 
				same ones that I had used in my childhood. If we are not going 
				to change them how are we preparing the new generation for the 
				future? <br>
				HE Tawfeeq: I agree with her. If we talk about education, a 
				month ago we had a World Bank presentation on the educational 
				system in Oman and they raised a lot of challenges based on the 
				data collected by the Ministry of Education. <br>
				<br>
				<b>Tim: So you are saying talk is cheap?</b><br>
				HE Tawfeeq: This is the problem with us. When we raise an issue, 
				it takes a lot of time to translate it into action as we are not 
				efficient. It takes a lot of time to modify our plans and to 
				execute them. And this is our main challenge. We know that there 
				are challenges in our education system but we have not heard or 
				seen any action from the Ministry of Education to improve 
				things. When Her Excellency, the Minister of Education visited 
				the Majlis A’Shura, there was a lot of criticism about stopping 
				the integrated curriculum, which has shown good results in terms 
				of competencies, mastering skills, like reading and writing, but 
				we are still waiting for the plan of action.<br>
				<br>
				<b>Tim to Nutayla in the audience: Would you like to see such 
				debates set specific target or a plan of action for governments, 
				ministers and the private sector? </b><br>
				Nutayla: Plan of action needs to be communicated to the public. 
				It should also have an element of ownership in terms of who will 
				be driving that plan. <br>
				<br>
				<b>Tim: Manal al Abdwani, would you sign up to that?</b><br>
				Manal: I would sign up to that. I agree that we are lacking in 
				follow-up though the name of my department is the department of 
				follow-up. In whatever we try, irrespective of whether we 
				achieve it or fail in it, we should go back and report it to our 
				stakeholders. I hope that with the new planning agency we find a 
				way for communicating the national plan to the citizens, 
				business community and all the stakeholders, and for telling 
				them on a periodic basis that this is what we were aiming at and 
				this is what we have achieved or failed in. I do subscribe to 
				that.<br>
				<br>
				<b>Tim: So what plan of action will you take away from this 
				session? What are you prepared to commit to till next year?</b><br>
				Manal: I would probably commit to sitting with a planning agency 
				and putting a format based on which each sector can spell out a 
				proper action plan for society. <br>
				<br>
				<b>Tim: Within a few months of this debate?</b><br>
				Manal: This should be realistically achievable within three 
				months of this debate. <br>
				Tim: So you have nine months of implementation time and you will 
				come back to us in a year’s time and tell us what you have 
				achieved. <br>
				<br>
				Manal: Absolutely. <br>
				HH Sayyid Tarik bin Shabib: On the sidelines I and His 
				Excellency Ali Al Sunaidi were having exactly the same 
				conversation. And, Tim, as you know one of the points that I 
				raised earlier regarding the relevance of these debates lies in 
				what we can take from one year to another. And His Excellency 
				has said that he would gladly take a paper with certain 
				recommendations based on today’s debate and champion it actively 
				within the higher levels of the government. He would take on the 
				views of the people in this room today and also those coming 
				online and help us to move forward.<br>
				(Audience applause) <br>
				<br>
				<b>Tim: HE Tawfeeq, are you happy with that?</b><br>
				HE Tawfeeq: As a starting point, why not? My request to His 
				Excellency, who is in the higher council as the deputy chairman, 
				is that we have to focus on education, if we need to improve our 
				standards. Education, education, education! We have to visit the 
				schools; we have to sit with the students…<br>
				<br>
				Tim: Its sounds like a politician…<br>
				HE Tawfeeq: Well I am a representative of the people and I see 
				the schools. We should not be result oriented, we should be 
				process oriented, and pay more attention to the environment and 
				the process of education. It is already costing us a lot. It 
				needs more attention and if we improve this process, I am sure 
				the outcome would be much better.<br>
				<br>
				Audience: I just wanted to put in a little perspective the 
				differences between developed and developing countries. Things 
				are not done overnight. If you see the track record of Oman over 
				the 42 years, a lot of things have been done with high speed. 
				Even in the education sector a lot has been done. I can give you 
				an industry perspective. I am the head of an SME. We are not in 
				oil and gas. We look for talent and we have had a lot of good 
				brains from Oman. What is exactly needed is a management 
				institute wherein we can pick up directly, train and develop 
				them. I am saying that the gap has to be bridged, between what 
				we want and what has been achieved.<br>
				<br>
				<b>One more from audience: My name is Ahmed Al Mukhaini. To what 
				extent would the Minister of Commerce and Industry regulate the 
				non-profit organisations?</b><br>
				Tim: Manal<br>
				Manal: At a personal level, I fully subscribe to his view. We 
				tried many times at the ministry to try and see if the 
				non-profit organisations are really rendering service to the 
				society and the country. So far, we have had little success in 
				getting approval for that. But I think that the whole economy 
				and the society are ready for such a question. Ahmed, if you are 
				planning to raise this question again, please come to the 
				ministry and we can put this up to the minister.<br>
				Tim: OK. Another question from the audience<br>
				<br>
				Audience: My name is Salman Al Lawati, I work for Oman Cables. I 
				just want to comment on the percentage of the new expatriates- 
				on the number of 100,000 and the 24 per cent. I think even if 
				you come next year, the number will be 200,000 and the 
				percentage would go down to 20 per cent. Why do we need to 
				analyse the percentages? Because, I believe 80 per cent is in 
				the construction sector which we cannot Omanise.<br>
				<br>
				I agree with Manal. If we go by sectors- just like oil and gas 
				banking and industry-banking is saturated with 90 per cent, well 
				done. Oil and gas is doing good. We need to focus on 
				manufacturing. That’s where we have more Omanis leaving their 
				job. We had 9000 Omanis left only last year. They went to the 
				government-created jobs. Why people are not staying in 
				manufacturing and why are they not leaving oil and gas and 
				banking jobs? Because, the government gives them some advantages 
				like interest-free loans and many other sops and benefits. So, 
				why don’t the government and the private sector and Majlis 
				A’Shura sit together and manage the people and tell them how to 
				work in the manufacturing sector. Because that’s where the 
				factories are coming and that is where the diversification is 
				going to happen…<br>
				Tim: Tawfeeq Al Lawati...<br>
				<br>
				Audience: We are having people leaving from the manufacturing…<br>
				Tim: OK. Let him answer this first<br>
				HE Tawfeeq: They are leaving the private sector because the 
				salaries given to them are based on the salaries accepted by the 
				expats. These salaries don’t satisfy the needs of the Omanis to 
				lead a minimum standard of living. So we made a proposal from 
				the Shura Council. We gave advice to the government to hire only 
				the required people, not to hire just to absorb unemployed 
				people from the street and give them a job.<br>
				<br>
				<b>Tim: (addressing the audience questioner) You want to come 
				back on that?</b><br>
				Audience: Yes, Mr Tawfeeq, I believe the salaries have been 
				increased from RO200 to RO300, but still we are losing people. I 
				believe it is not because of the salaries. We need something…<br>
				<br>
				Tawfeeq: There is basic minimum wage required…<br>
				Audience: Even if you maintain that, here we are dealing with 
				unskilled people who have just left high school. You are not 
				dealing with university people. We need something different. We 
				need a catalyst to be kept in the industry sector so that 
				everyone will go to the industry sector and this what the 
				government need to do.<br>
				<br>
				Tim: Abdul Aziz<br>
				Abdulaziz: I agree that we have to make this sector more 
				attractive to work in. It’s not salary. It is also about other 
				motivational factors that will make them work in the 
				manufacturing sector. There are so many things that can be 
				offered to individuals such as coaching. Most of the people 
				would not know the job. But if we coach them, it will help them 
				in the long run.<br>
				HE Tawfeeq: Excuse me, Tim. In our proposal, we recommended to 
				the government to add RO150 above the salary, give a piece of 
				land and an interest free loan for those who are joining the 
				private sector. This is just to motivate the Omanis to stay and 
				work in the private sector.<br>
				<br>
				Tim: Stephen Thomas<br>
				Stephen: I think you need to look at the different companies and 
				sectors in the private sectors from where there has been a 
				migration of people to the public sector and those companies and 
				sectors from where there has not been. Now why do people join a 
				company? There will be brand equity of that company, of what it 
				is like and how progressive for an employer it is, how much it 
