<p class="style15" style="line-height: 14px">
Through its recently released sustainability report, Omantel seeks to ensure 
that its growth is sustainable, by considering the impact of its business 
operations on the society and environment<br>
<br>
Omantel has launched its first Sustainability Report at a special event held at 
its headquarters under the auspices of His Excellency Yahya bin Said al Jabri , 
chairman of Special Economic Zone at Duqm (SEZD) Authority and chairman of the 
Capital Market Authority (CMA). A number of VIPs and senior executives attended 
the event. The report themed “Because We Care” is the first of its kind in the 
telecommunication sector in the Sultanate, and was developed in accordance with 
the Global Reporting Initiative (GRI) framework, version 3.1.
<br />
						<p class="style15" style="line-height: 14px">
“No doubt that the launch of this report mirrors the sincere efforts made by 
Omantel to achieve more transparency and openness in communication with the 
different stakeholders,” said HE al Jabri. “The move will enhance the confidence 
of all stakeholders in the company businesses and operations. CMA encourages all 
companies especially those listed with Muscat Securities Market to follow the 
same trend. In this connection, CMA has given instructions to all MSM-listed 
companies that they should include a miniature report on the CSR activities 
carried out by each company in the annual reports represented to the 
shareholders at the end of the financial year. By publishing such integrated 
sustainability report, Omantel has become the leaders in the field and is 
setting a trend for Oman’s corporates.”<br>
<br>
The launch ceremony included a presentation by Dr Amer bin Awadh Al Rawas, CEO 
of Omantel, on the major themes covered by the report namely ‘We are at Omantel 
, we are Growing, we are Caring and we are Together’.<br>
“It is a pleasure today to launch the first sustainability report 2012, which is 
also the first of its kind in the telecom sector in the Sultanate,” says Dr al 
Rawas. “The report showcases the company’s commitment to transparency and 
enhancing communication with the different stakeholders. We are proud of our 
history, and as the largest national telecommunications company, we look towards 
the future with confidence; and we will continue to ensure that our growth is 
sustainable, based on careful consideration of the impact of our business 
operations on all stakeholders, including the society and the environment.”<br />
						<p class="style15" style="line-height: 14px">
Over the years, Omantel has embarked on a series of landmark corporate social 
responsibility (CSR) initiatives, in line with the national objectives of 
connecting the nation and promoting digital literacy, he added. “We are the 
technology partner for the Information Technology Authority (ITA)’s National PC 
initiative, and we have also committed to expand our network to include remote 
villages throughout the country. Furthermore, last year we instituted the 
Omantel Business Excellence Awards, aimed at supporting small and medium 
enterprises throughout the country, he furthered.”<br>
<br>
Commenting on the role played by Omantel staff, Al Rawas adds, “Our employees 
have been an absolutely critical enabler for our achievements, and, going 
forward, we will further enhance their conditions to succeed. We strive to be a 
learning organisation, which provides opportunities for growth as part of an 
engaging, rewarding and performance-based culture. Choosing ‘Because we care’ as 
a title for the report reflects our endeavors to meeting the interests of all 
stakeholders.”<br>
<br>
The report will be published annually to advise all stakeholders about the 
company’s economic, social and environmental initiatives.<br />
