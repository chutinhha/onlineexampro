<b>Using the tried and tested 3R</b>
<br />
<br />
 Bechtel Enka Bahwan (BEB) Consortium which is designing and building the new $1.8bn passenger terminal complex at Muscat International Airport implemented the tried and tested waste Reduction, Reuse, Recycling programme to reduce the carbon footprint of the construction works
The Muscat International Airport project is a mega project worth billions of dollars. The $1.8bn passenger terminal complex being built by Bechtel Enka Bahwan (BEB) Consortium has about 12,000 workers working on the construction site at peak production. The BEB consortium of companies decided to implement the simple waste reduction measures namely Waste Reduction /Reuse / Recycling Programme in order to reduce the carbon footprint of the construction works.
The goal was to maximise the opportunities to reduce, reuse and recycle waste products/ materials during the construction period in order to prevent waste being disposed of in Oman’s landfill sites and thereby effectively reduce the carbon footprint of the project.

<br />
<br />
 The success of the programme was measured by tracking waste management information and procurement data (including disposal records, invoices etc) and daily site based observations. By analysing the volumes of waste diverted from Oman’s landfill sites, it was possible to demonstrate the reduction in the carbon footprint.
<br />
<br />
Metal (rebar) and concrete blocks were recycled to make ‘Candy cane’ uprights for pedestrian / vehicle routes in order to provide suitable segregation, while wood was to be recycled to make wooden frames for cable management on site and for additional support for formwork, notice boards and signage. Further, all the waste concrete was used for pathways, lay-down areas and general storage areas as well as for building containers for hazardous materials.
<br />
<br />
Due to the simplicity, low cost and success of the waste reduction measures implemented at this construction project, the same initiatives and principles can be applied to all other construction projects throughout Oman.
