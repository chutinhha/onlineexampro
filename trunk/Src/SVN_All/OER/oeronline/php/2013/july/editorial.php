<br />
World Environment Day (WED) is an annual event that aims to be the biggest and most widely celebrated global day for positive environmental action. WED activities take place all year round, culminating on June 5, every year. WED celebration began in 1972 and has grown to become one of the main vehicles through which the UN stimulates worldwide awareness of the environment and encourages political attention and action.
<br /><br />
Through WED, the UN Environment Programme has been successful in personalising environmental issues, enabling people to understand their responsibilities and realise their power to become agents for change in support of sustainable and equitable development. WED is also a day for people from all walks of life to come together to ensure a cleaner, greener and brighter outlook for themselves and future generations.

<br />
<br />
The theme for WED 2013 -- Think.Eat.Save -- focuses on food wastage. According to studies, one third of all food production or 1.3bn tonnes gets lost or wasted in the food production and consumption systems worldwide. Food waste is a global problem that has negative humanitarian, environmental and financial implications. It is also a problem that can be combated with relative ease, with a few simple changes to our habits. 

<br />
<br />
Over the last four years, WED in Oman has been synonymous with Oman Green Awards (OGA), the one of its kind environmental platform that recognises, showcases and felicitates exemplary environment conservation efforts by companies and individuals. This year’s OGA saw a cross-section of companies submitting their entries. This edition does an in-depth analysis of the awardees of OGA 2013. We hope that these torch bearers will encourage others to join the environment movement in the Sultanate.

