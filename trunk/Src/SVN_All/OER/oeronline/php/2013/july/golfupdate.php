						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">Golf 
					passport for summer launches</font></b><br>
						<font face="Verdana" size="2"><b><br>
						For the first time, Muscat’s three grass courses have 
					partnered up to provide golfers with a way to keep their 
					game interesting throughout the summer months</b></font><p align="justify" style="margin: 3px 10px"> 
					&nbsp;<p align="justify" class="style13">
				<img border="0" src="images/golf_update_1.jpg" width="132" height="263" align="right" vspace="6" hspace="6" style="margin-top:-5px;">
				Almouj Golf has joined up with Ghala Valley and Muscat Hills to 
				offer a special summer ‘golf passport’ for golfers looking for 
				variety in their game this summer. This passport allows golfers 
				to play on all three 18 hole grass courses, regardless of 
				membership.<br />
<br />
				Golf passport holders will be able to use the passport to redeem 
				four 9-hole games on each golf course, giving the perfect offer 
				for golfers looking to challenge themselves on new courses.<br />
<br />
				“We are really pleased to be able to join together with our 
				Muscat golfing colleagues to provide people with this offer,” 
				said Simon Duffield, general manager of Almouj Golf. “While the 
				summers here can be quite challenging to play in, golfers in the 
				Gulf are used to varying their game to ensure that they can play 
				in the higher temperatures we experience. Including 12 nine-hole 
				rounds for RO90, the passport is specially formatted so that 
				golfers get the best out of their summer game.”<br />
<br />
				Mubarak Hill, general manager, Ghala Valley Golf Club, said, 
				“Realising how well the three golf courses complement each other 
				has presented this unique possibility here in Muscat. We are 
				delighted to have joined hands with our golfing colleagues in 
				this first of many collaborations to come. We hope the golfers 
				in Oman and the region take advantage of this opportunity to 
				encourage more of such offers.”
<br />
<br />
Ray Stopforth, director of Golf at Muscat Hills, commented, “Muscat Hills Golf & Country Club is pleased to be associated with the other two golf courses for the Summer Golf Passport offer. It is an excellent offer for playing golf during the summer and experiencing the uniqueness of all three green golf courses in Oman. We anticipate this offer to encourage the social golfer to play golf more during summer and thus increasing the participation numbers and community in golf.”
<br />
<br />
Golf passports can be purchased at Almouj Golf, Ghala Valley and Muscat Hills and are valid from June 1, to September 31.&#8194;


