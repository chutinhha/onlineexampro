					<b><font face="Verdana" size="4" color="#C00000">Growing 
					golf at grassroots level</font></b><br>
						<font face="Verdana" size="2"><b><br>
						Oman Golf Committee’s junior golf strategy is aimed at 
					building capacity through a national school initiative and 
					growing a base of talented national players</b></font><br /><br /><center>
					&nbsp;</center>
					<p align="justify" style="margin: 3px 10px"> 
<font size="1" face="Verdana">

<img src="images/oman_g_1.jpg" align="left" hspace="4">Oman Golf Committee (OGC), which was founded in 2009, is taking the lead to ensure that Oman builds a golf industry that is sustainable and properly regulated. To achieve this, the committee has identified the importance of building strong foundations through junior golf. OGC’s vision is to ensure that in the long run every child in Oman has an equal opportunity to experience golf. Its mission is to build a foundation in golf that will give all children the opportunity to play and enjoy the sport. 

<br />
<br />
 Towards achieving this goal, the OGC wants to create an environment that allows beginners to learn and serious golfers to strive to the top. Oman golf committee aims to build a population of local golfers which will support the domestic clubs to ensure golf flourishes and is sustainable in the long term.
 
<br />
<br />
 The pyramid model shows the OGC junior golf strategy to build capacity through a national school initiative, to feed young Omani talent through the junior programme and grow a base of talented national players in development and elite squads. The junior programme has already established a track record of growing young golf talent since 2009. More than 500 children have participated since the start and some capable young Omani golfers have grown from the programme. Oman (both the national junior team and individually Ahmed al Balushi) bagged silver in the GCC Golf Championship held at the Almouj golf course in Muscat in February.
 
<br />
<br />
 “Our junior golf programme has proved a success; we now believe that by dedicating ourselves to expanding this programme to a larger market segment, we should grow more golfers,” said OGC Chairman Mundhir al Barwani. “I believe we need to have a collective approach with all stake holders, we could be more effective and all benefit. Included in OGC next three-year plan is the school initiative where we hope to train the trainers- this will, we hope, have participating schools’ PE teachers trained so that basic golf can be included in the curriculum. The initial steps are already on the way.”
 
<br />
<br />
 To fulfill the junior strategy, OGC needs support from several entities including ministries, international organisations, golf clubs, local corporations and prominent businessmen. The committee will also need the help of volunteers who love the game and want to contribute. It believes that with the support from the above groups it can achieve far more and make golf a leading sport in Oman.&#8194;
