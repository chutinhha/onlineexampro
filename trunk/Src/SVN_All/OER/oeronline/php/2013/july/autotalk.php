				<font face="Verdana" size="1">
				&#8207;The 2013 Infiniti M sets the bar high for mid-size performance 
				luxury sedans. As Infiniti’s halo sedan, the Infiniti M embraces 
				the essence of all things Infiniti – style, performance, luxury, 
				craftsmanship and technology. The Infiniti M is offered in both 
				M37 and M56 model designations. The M37 features a refined 
				328-horsepower 3.7-litre V6 engine, while the M56 utilises a 
				402-horsepower, 5.6-litre V8. Both engines feature Infiniti’s 
				advanced VVEL (Variable Valve Event &amp; Lift) system and the M56 
				engine also utilises Direct Injection Gasoline (DIG) technology. 
				All models are equipped with a standard 7-speed automatic 
				transmission and are available in rear-wheel drive. 
				<br>
				<br>
				<b>&#8207;</b>The Infiniti M continues as a showcase for 
				Infiniti’s advanced, user-focused technologies, including the 
				Infiniti Drive Mode Selector, a four-mode driver-selectable 
				control of throttle and transmission mapping that helps optimise 
				vehicle performance; Blind Spot Warning (BSW), which helps alert 
				the driver if another vehicle is detected in the blind spot area 
				etc.<br>
				<br>
				Other features include the Blind Spot Intervention, a world’s 
				first system that provides selective braking if the vehicle 
				approaches an adjacent lane while another vehicle is detected in 
				the blind spot area to help the driver return the vehicle back 
				toward the centre of the lane of travel. Another interesting 
				feature is the Forest Air system, which helps reduce the 
				intrusion of unpleasant odours into the cabin and provides a 
				more natural breeze-like airflow. The M offers a quite cabin 
				with its Active Noise Control, which generates “canceling” sound 
				from four door speakers to reduce unpleasant engine harmonics. 
				The cabin noise levels are monitored by two microphones located 
				in the M’s ceiling, with no action needed from the driver and 
				passengers, except to enjoy the quiet, comfortable cabin with 
				music from the 10 or 16-speaker Bose Studio Surround premium 
				audio system.
				<br />
				<br />
				<strong>&#8207;Expressive styling </strong>
               <br />
               	Unlike other performance luxury brands that prioritise the 
				impression their vehicles make on others, Infiniti’s intention 
				is different – with the focus centred on the individual driving 
				the car. The goal of the Infiniti M interior is to present an 
				elegant yet functional design that combines artistry with 
				precision, comfort with innovation – all in support of enhancing 
				driving performance and passenger enjoyment. The exterior of the 
				M combines balanced, rear-wheel drive proportions and stance 
				with a long, low hood and high rear deck to create an efficient 
				aerodynamic shape – while also providing a roomy cabin. The M 
				Sedan’s sporty appearance is highlighted by the deep body shape 
				of the fenders and doors, which give it a powerful look unlike 
				anything on the road.<br />
               <br />
               	The wave-style doors are constructed from aluminum for lighter 
				weight, despite the material’s difficult formability. The 
				stamping dies for each door are hand polished to maintain their 
				precision and every door undergoes an exacting human inspection, 
				providing a handcrafted level of quality. The exterior optimises 
				both luxury and sport styling cues – with an expressive, 
				signature Infiniti double-arch upright low grille and 
				crystal-look headlight clusters, large aluminum-alloy wheels, 
				and a wave-like trunk with a high aerodynamic performance that 
				works like a spoiler to increase downforce. All in all, the 
				Infiniti M provides a confidence-inspiring environment for 
				driver and passengers.&#8194;</font><b><p align="center" style="margin: 10px; ">
