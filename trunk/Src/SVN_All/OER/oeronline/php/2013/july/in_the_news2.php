					<p class="style15" style="line-height: 14px">
The Zubair Corporation officially launched its new initiative- The Zubair Small 
Enterprises Centre (Zubair SEC)-to support the national economy by empowering 
emerging business ideas and supporting small enterprises. The Zubair SEC will 
provide all the necessary tools to help fledgling ventures grow, succeed, and 
become stable contributors to the Omani economy. The corporate social 
responsibility initiative from the company demonstrates a long term strategic 
vision in harmony with the Government’s vision for the economy.<br>
<br>
The launch of the Zubair SEC followed by a workshop for the aspiring small 
enterprise applicants was attended by Rashad Al-Zubair, chairman of The Zubair 
Corporation, Abbas Al-Humaid – advisor to the managing director of The Zubair 
Corporation, and Hani Obeid – senior advisor to the Zubair Small Enterprises 
Centre and acting CEO of the new centre.<br>
<br>
Rashad Al-Zubair gave an overview of the strategic objectives of the centre and 
detailed how it aims to positively transform the common understanding of the 
entrepreneurial and small business culture in Omani society. The briefing served 
to highlight the increasing importance of small businesses in a maturing 
economy. Commenting on the formation of the centre, Rashad said the inauguration 
of the Zubair Small Enterprises Centre was in line with His Majesty Sultan 
Qaboos bin Said’s Royal Directives. He added that the Zubair SEC team of experts 
and specialists have been very selectively formed. They are all experienced in 
developing small enterprises in particular, and they will function under the 
supervision and guidance of Hani Obeid who has more than 20 years of practical 
experience in the field of management consulting.<br>
<br>
Abbas Al-Humaid – advisor to the managing director of The Zubair Corporation – 
presented the framework of the new centre and the support it would give to the 
participants. “The Zubair SEC is a professional non-profit centre that is 
operating independently as a business incubator for small businesses and 
entrepreneurs, to help lead them to sustainable success,” said Al Humaid. “The 
centre will be run by international experts and business consultants who will 
work together with the entrepreneurs and small business owners and will fully 
utilise the strength of The Zubair Corporation’s business connections, locally 
and internationally. This will provide the suitable climate and an appropriate 
environment for small enterprises, in addition to consultancy services to help 
them grow and be sustainable in the long run.”<br>
<br>
Elaborating on the focus of the centre, Al-Humaid said “The focus will be on two 
main areas; the first being the selection of ten entrepreneurs and their 
projects, services or ideas who will be aided by professional consultancy 
services including legal, financial, HR, administration and marketing. The 
Zubair Small Enterprises Centre will help build individual strategies for each 
project to enable sustainable success.” An interest free loan will also be made 
available to each qualifying small enterprise to provide start-up capital. 
Moreover, the centre will ease the participants access to additional financial 
funding from other sources available in the market, if need be. The centre will 
also examine if there is a market among Zubair Corporation’s companies for their 
products or services.&#8194;<br>
