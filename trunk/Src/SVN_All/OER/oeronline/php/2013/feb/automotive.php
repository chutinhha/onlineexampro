						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Turbocharged growth</font></b><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="2">
				<b>David B Aziz, CEO, MHD talks about new products, facilities 
				and future plans in a freewheeling conversation with Mayank 
				Singh</b></font><p align="center" style="margin: 10px; ">
				<img border="0" src="images/automotive01.jpg" width="525" height="418"><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<b><br>
				How was the year 2012 from a business point of view and what 
				are your plans for financial year 2013?<br>
				</b>Business was challenging in 2012, not just in the automotive 
				sector but overall as there are a number of issues and concerns 
				globally, though Oman is better placed than other countries in 
				the region. One of the reasons why the year 2012 proved to be 
				challenging was because there was a change in some of our 
				models, like the Land Rover. As a result we are seeing an 
				extremely high demand for our new models. We have also been busy 
				with the development of our new showroom facilities at Azaiba 
				for our new brands, which are becoming big volume drivers. 
				Overall, the year was not bad but we will be looking at better 
				results this year. It’s been challenging, but this year we are 
				quite happy and optimistic about the product range that we have, 
				our enhanced facilities and the investment that we have made in 
				our Omani staff. Our Omanisation is on the increase in the 
				automotive sector and all this could give us fruitful results in 
				the coming months. The government has planned a RO13bn 
				investment in infrastructure development projects in its ongoing 
				five-year plan and this should boost commercial and passenger 
				vehicle business. These investments should help and benefit 
				every business in the country.<br>
				<br>
				<b>MHD has invested in upgrading its showroom facilities. How is 
				this going to enhance the customer experience? <br>
				</b>If you look at our Jaguar showroom you will see that we have 
				developed a multi-storied showroom. It’s a state-of-the-art 
				showroom for our high profile Jaguars with all the facilities. 
				On the top of it, we have the sitting facilities for our new 
				Range Rovers. This is backed up by a new refurbishment facility 
				for Land Rover and Jaguar in our Sohar showroom. To conclude we 
				have the state-of-the-art facility for our MG, BYD and Luxgen at 
				Azaiba 2 which includes a full commercial servicing area for our 
				commercial products.<br>
				<br>
				<b>MHD automotive has acquired new brands like MG, BYD and 
				Luxgen in the last few years. What is the idea behind acquiring 
				these brands? <br>
				</b>The idea is to provide the best service to the Omani 
				community. We wanted to touch people’s life on the commercial 
				side so we developed brands such as Ashok Leyland, JMC and CMC. 
				We are also looking at some heavy duty trucks coming through. On 
				top of that we wanted to have products in the volume sector, in 
				the mid-level range. As always we looked at the best products, 
				with a view to provide our customers with the best at the most 
				reasonable prices. Keeping this in mind we acquired three brands 
				– the MG brand from the UK, Luxgen from Taiwan -- I am proud to 
				state that MHD Oman is the first in the world to sign an 
				international distributorship agreement with Luxgen outside 
				Taiwan.<br>
				<br>
				Moreover, we set up the first Think Plus Luxgen showroom in the 
				Gulf. It’s a very high-quality product and then we have the BYD 
				a very popular car which has Japanese engineering with German 
				technology. The vehicle has been in the market for the last 
				three years and has proven to be very good, its pricing is very 
				competitive and its quality is improving year-on-year and the 
				response from the public is getting better commensurately. We 
				are in a position to say that we now offer a full range of 
				products which include prestigious cars, commercial products and 
				medium range volume vehicles. On top of this we have a very 
				strong used car operations, which has been going on for 10 years 
				now which offer quality used cars to our customers.<br>
				<br>
				<b>The 2013 Range Rover has been one of the most anticipated 
				launches of the year. What are your thoughts on the new Range 
				Rover?<br>
				</b>It’s absolutely phenomenal. The vehicle has gone from 
				strength-to-strength and we are very pleased with that. The new 
				vehicle comes with a full aluminium body, which is 420 kilograms 
				lighter than the previous model. It is equipped with a 510 brake 
				horse power super charged engine, with a new eight change 
				selective gear box. It has all the top refinements, state-of-the 
				art entertainment system, aerospace technology, the finest 
				leather and quality systems that one can aspire for. The 
				response has been phenomenal, with sales being way above our 
				product availability. We have started deliveries and there are 
				more to come in the coming weeks and months. We also expect an 
				increase in our entire product line-up, including the Land Rover 
				and the Evoque which is proving to be an excellent vehicle. The 
				new shaped LR2 2013 and LR4 continue to grow, and we have the 
				Range Rover sport which has been our all time best seller in all 
				its derivatives for years.<br>
				<br>
				<b>The Range Rover Sport is being phased out by the end of 2013. 
				Will that have an impact on your sales?<br>
				</b>It will have some impact as people are always on the lookout 
				for new models; but as things stand it is selling very well and 
				we are a long way away from new model as of now.<br>
				<b><br>
				How has the performance of brand Jaguar been in Oman in the last 
				couple of months? </b><br>
				Jaguar continues providing us with top quality and very refined 
				vehicles. The XF is coming with new engine derivatives -- it has 
				a new two litre, three litre turbo engines and five litre 
				engines. We also have the new XJ which is a long wheel base 
				vehicle with new engine derivatives. It offers the most refined 
				and luxurious interiors in its segment. There is a new F type, 
				which will be joining the brand and other models soon. New 
				colours, trim and accessories along with our enhanced showroom 
				facilities have been getting us a very positive customer 
				response.<br>
				<br>
				<b>Is MHD strengthening its distribution network across the 
				Sultanate? <br>
				</b>We have a presence in Muscat, Sohar and Salalah. The Sohar 
				showroom has been refurbished with Land Rover and Jaguar 
				specified specifications and furniture. As new models come up we 
				are always on the move. We are looking at facilitating our 
				customer needs in other places too as our business grows and 
				develops.<br>
				<br>
				<b>Have you strengthened your after sales service and parts 
				availability in the last one year? <br>
				</b>We have cut our spare part prices by 35 per cent and have 
				been getting a lot of gratitude and thanks from the entire Omani 
				community for this move. This across the board price reduction 
				was done to help the people of this country six months ago. The 
				decision was taken in consultation and association with Consumer 
				Credit Protection agencies and it was announced that all our 
				parts across all our brands were to be slashed by 35 per cent. 
				This makes us the most competitive company in the Gulf, from a 
				parts pricing perspective. We see this as a national 
				responsibility and we wanted to do something that would help and 
				support people.<br>
				<br>
				<b>Is Omanisation a focus area for MHD. If yes, in what ways are 
				you working towards this goal?<br>
				</b>As a company we have developed the MHD Training Institute in 
				association with the Ministry of Manpower in Darsayt. At the 
				institute we take Omanis, train them in different skills like 
				technical, engineering, automotive, so that they can take on 
				jobs in various sectors of the economy. We train and certify 
				people not only for MHD but also for other companies. We are 
				working closely with the Ministry of Manpower and Labour. The 
				Institute has won numerous awards for its dedication and service 
				to the community. Within the organisation we have a number of 
				Omanis who are reaching high managerial positions.&#8194;</font></td>
