<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">The Qatar National Bank (QNB) has 
evolved into a global bank with $100bn in assets and a footprint in 25 
countries. QNB’s purchase of National Societe Generale Bank (NSGB) will close in 
mid-2013, if the Egyptian central bank and securities regulator approves the 
deal. This is a transformational acquisition for QNB, as NSGB, with its $10.50bn 
assets and 160-branch network, significantly increases its Egyptian banking 
footprint. In fact, assuming the NSGB deal is approved, Egypt will be 10 per 
cent of QNB loans and deposit. This adds higher risk profile to Qatar’s flagship 
bank since it is now exposed to political and currency risks that have not 
dampened since the election of President Mursi and the drafting of a new 
constitution. QNB has also increased its strategic stakes in Abu Dhabi’s CBI, 
Iraq’s Mansoor Bank and Libya’s Bank of Commerce and Development. It makes 
perfect strategic sense for QNB to build a pan-Arab regional network that now 
accounts for 30 per cent of total assets and 20 per cent of net profits but this 
exposes the bank to the political and social risks of the Arab spring.<br>
<br>
Yet Egypt is the most populous nation in the Arab world; Iraq is an emerging oil 
and gas superpower; Libya is North Africa’s wealthiest state – all potential 
growth engine for QNB since banking penetration rates are miniscule in all three 
Arab states. It is also not coincidental that Qatar is playing an increasingly 
activist role in the wider politics and economics of the Arab world. Qatar’s 
recent decision to deposit $2.5bn in the Egypt Central Bank to boost its hard 
currency reserves on the eve of an IMF team’s arrival in Cairo is a testament to 
Doha’s new role in Arab financial diplomacy and the QNB – NSGB deal can only 
benefit once Egyptian economic growth resumes.<br>
<br>
A major catalyst for QNB’s acquisition spree is its low cost funding, given its 
role as the flagship bank of Middle East’s leading LNG exporter and the owner of 
world’s third largest natural gas reserves. However, Qatar has 18 banks 
operating in a banking market that is nowhere near the size of the UAE or 
Kuwait, let alone Saudi Arabia. Moreover, Qatar’s economic growth rate has 
fallen to 5-6 per cent, down from the 10-12 per cent hyper growth rates two 
years ago. So the strategic ballast to expand abroad is compelling for QNB 
management.<br>
<br>
QNB obviously benefits from the Qatari government’s $135bn project and 
infrastructure spending programme, particularly the stadiums, hotels, rail 
networks and airports needed for the 2022 soccer World Cup. This is a EPS growth 
ballast for QNB in the next decade as the de facto government bank in Qatar. 
Qatari shares were the worst performing in the GCC (ex Bahrain) in 2013, with 
bank shares hit hardest. QNB missed its earnings estimates though it has 
increased its dividend to 6 Qatari riyals a share. Yet QNB boasts a capital 
adequacy ratio of 21per cent and it is now the leading bank in the Arab world, 
an argument for a premium valuation, given its sovereign ownership and evolving 
emerging markets finance growth model with a potential secular ROE of 15 per 
cent. This valuation rerating will only manifest itself once QNB successfully 
closes the NSGB deal and Egypt’s economic crisis improves, possibly after the 
IMF loan is completed. If I am right, QNB shares have the potential to rise as 
high as QR 170-180 in the next twelve months.<br>
<br>
Apart from QNB, I believe Industries Qatar is another blue chip core holding in 
Qatar. This is Qatar’s leading petrochemical, fertiliser and steel conglomerate 
whose projects benefit from sovereign backing and the lowest coast energy 
feedstocks in the Middle East. Industries Qatar’s project pipeline in 2013-14 
means capex declines while earnings rise. However, the shares could drift tower 
into the QR 140-150 range as the global pricing environment for both fertiliser 
and steel is still weak, though the economic rebound in China will clearly act 
as a catalyst for price rises. A dividend yield in the 5 per cent range should 
also attract regional fund managers to accumulate Industries Qatar if it trades 
in 140-150 Qatari riyal range, in Doha.<br>
<br>
Saudi banks also offer compelling value in 2013 as beneficiaries of 15 per cent 
loan growth and the $500bn Saudi infrastructure spending programme. SAMBA 
Financial is the leading corporate and investment bank in the kingdom, with a 
stellar balance sheet, a 18 per cent Basle Tier One capital base and a 
loan/deposit ratio of only 68 per cent. Hit by provisions in 2012, SAMBA can 
well rise in 2013 as it trades at a mere 1.4 times book value and 8 times 
earnings. An optimal buy/sell range for the bank could well be 45-56 Saudi 
riyals in 2013.</font></td>
