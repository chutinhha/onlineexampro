						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Non-conventional water for irrigation</font></b><p align="justify" style="margin: 3px 10px; line-height:17px"><b><font face="Verdana" size="2">
                		<br>
						The Ministry of Agriculture’s project for utilising 
						treated wastewater for agriculture production in Saham 
						is aimed at conserving water resources and exploring new 
						possibilities for irrigation</font></b><p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
						At a time when world population has exceeded 7 billion 
						and the demand of water for domestic, industrial and 
						agricultural purposes is steadily increasing, 
						sustainable use of non-conventional water resources for 
						agriculture is the need of the hour. Reusing 
						non-conventional water resources is a win-win solution 
						because it helps solve two problems simultaneously: safe 
						wastewater disposal and more water to irrigation. An 
						ambitious step towards this direction in Oman was the 
						Ministry of Agriculture’s project for utilising treated 
						wastewater for agriculture production in Saham.<br>
						<br>
						Developed by the Directorate General of Agricultural &amp; 
						Livestock Research, the project seeks to explore 
						non-conventional water resource for agriculture 
						development and reduce the pressure on groundwater 
						resources. The project is intended to utilise the vast 
						amount treated wastewater and cultivable lands in the 
						country for producing seasonal forage crops. This was 
						done after evaluating forage crops for winter and summer 
						(sorghum, maize, and barley) and studying the impact of 
						tertiary treated wastewater on the soil at the targeted 
						sites. Initially the directorate has worked out a plan 
						to cultivate crops for two years and tested the 
						commercial viability of the pilot project. The project 
						has turned out to be very successful and the results 
						were fantastic, although two years of constant 
						observation and data collection were an uphill task. 
						Sorghum was the most profitable crop.<br>
						<br>
						Instead of one crops, the Ministry has selected three 
						crops for observations. The changes the crops had 
						undergone during the two seasons were properly observed. 
						Season-crops rotation-economic model on actual cost 
						basis was very innovative method to estimate results 
						more accurately. All records of yield – sales-input cost 
						etc. were worked out for each crop and for each season.</font><p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
						<br>
						Thus six economic models were developed, two for each 
						crop and then aggregate level results are worked out 
						which has long lasting validity and can be used as 
						sustainable cultivation of crops with treated 
						wastewater. However, some government support would be 
						essential to promote this experiment and ensure the 
						supply of treated wastewater. In view of “food security” 
						which is top on the agenda, promotion of such projects 
						utilising the treated wastewater for increasing fodder 
						supply is of paramount importance.<br>
						<br>
						</font><font face="Verdana" size="2">
						<b>Treatment Plants<br>
						</b></font><font face="Verdana" size="1">
						According to information provided by Oman Wastewater 
						Services Company (OWSC), there are 11 wastewater 
						treatment plants of the company in Muscat with average 
						production to about 41,800 and 42,750 and 94,740 m3/day 
						during the years 2008, 2009 and 2010, respectively. The 
						forecast for the period 2011-2016 is expected to 
						increase output of treated wastewater by an increase in 
						demand by the Muscat Municipality as a result of which 
						the estimated average quantity of surplus water would be 
						between the 17,943 m3/day in 2011 and 21,993 m3/day in 
						2016. However, there are 48 wastewater treatment plants 
						in other governorates of the Sultanate supervised by the 
						Ministry of Regional Municipalities &amp; Water Resources. 
						Moreover, figures from the Ministry of Regional 
						Municipalities and Water Resources show that the total 
						capacity of the current number of stations (48 stations) 
						was expected to be 38,010 m3/day whereas the average 
						amount of water as treated effluent from 48 stations was 
						estimated to be only 8,665 m3/day taking advantage of 
						municipal needs and landscaping as 4,204 m3/day which 
						accounts for 48.5% of the average amount of treated 
						wastewater. Hence, there is a need to conserve water 
						resources and explore new possibilities for reusing 
						treated wastewater for irrigation.&#8194;</font></td>
