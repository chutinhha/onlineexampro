<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">The art of good storytelling is 
often overlooked in today’s more complex communications environment. A well 
narrated story is exactly what it takes to break through, grab journalists’ 
attention, and ultimately engage with a target audience. For instance a press 
release can be packed with accurate data and feature impressive third party 
testimonials, but without </font><i><font face="Verdana" style="font-size: 8pt">
a</font></i><font face="Verdana" style="font-size: 8pt"> good story at its 
heart, it will lack the power to influence opinions, beliefs and behaviours.<br>
<br>
At a business event recently someone asked: “I hear about PR all the time, but 
what is it that you people actually do?” An easy enough question, but I was 
mindful that my answer shouldn’t get bogged down with corporate buzzwords or 
goobledygook. I replied “Public Relations is the practice of managing 
communication between an organisation – or an individual – and their audience.”
<br>
<br>
To understand the central role of PR, let’s start at the beginning. Like many 
things in life it’s best to have some sort of a plan, or in this case a strategy 
on how and what the organisation is going to communicate - and to whom. Before 
one print advertisement is designed, one press release is written or a media 
plan prepared – indeed before any communications materials are produced at all – 
they first need to be created according to a central theme or strategy. We 
regularly meet with clients around the boardroom table solely to figure out how 
objectives can be met and how their story can be told. The agreed approach, 
objectives and tactics are then formed into a plan of action – the 
communications strategy. Organisations have a strong desire to communicate their 
story, so after the plan is agreed the next step is content - or creating the 
story relevant to the medium. For instance with social media content, it’s very 
much about understanding what will drive the conversation to illuminate the 
message in a remarkable and credible way. <br>
<br>
Good communication professionals understand the value of strategically generated 
messages and content that can be applied ‘top-down’ across all media platforms. 
This means that messages across all touch points such as advertising, news 
releases, social media, online and business or community engagement that fit to 
a pre-agreed plan and are consistent.<br>
<br>
The approach to strategically plan and map out all communications results in a 
consistent voice from the organisation to their audience – across all 
touch-points. Within this broad mandate it’s the (admittedly not always easy) 
role of PR to generate engaging stories with coherent and relevant information. 
The briefing from the client and the subsequent development of the information 
requires a close and ‘trusting’ relationship in order to truly understand the 
communication objectives, obstacles to success and to gain real insight into the 
organisation. <br>
<br>
In this age of media proliferation, news outlets are becoming more reliant on 
the PR sector for their stories than ever before. This is where it is vitally 
important for those of us to maintain productive and ethical working 
relationship with journalists and media outlets. These positive relationships 
are vital in order to generate trust and a flow of information to enhance 
awareness and credibility for their client organisation. This comes in the form 
of editorial content in newspapers, magazines, TV, radio or online – content 
which is not paid for.<br>
<br>
News organisations and online outlets are increasingly asking for fresh stories 
and engaging information, and in the age of the ‘60 minute news cycle’ the 
organisation who can provide strategic and high quality editorial content in a 
timely fashion wins the race. At the same time ironically, newsroom resources 
are shrinking evermore as a result of reduced revenues from subscriptions and 
advertising. The increasing popularity of free online news content coupled with 
the literal explosion of media outlets, blogs and social media only compounds 
these trends further. Within these changing landscapes, the lines between paid, 
owned, earned and shared media are blurring rapidly, with the need for fresh and 
interesting stories driving them all.<br>
<br>
Just as the news media are coping with the information age, so too is the PR 
sector. Leading PR firms now position themselves as trusted senior advisors and 
are viewed as equally important to the success of an organisation as management 
consultancies, investment bankers or financial advisors. In this trusted 
advisory capacity they also provide a public affairs function to facilitate 
dialog between different organizations, manage reputations of companies and 
individuals, conduct press conferences and media events, offer important counsel 
for corporate social responsibility and provide guidance and advice for media 
outreach in the time of crisis. <br>
<br>
Globally, the business of PR has become huge, and is now recognized to be 
growing faster than general advertising and marketing budgets. Global revenues 
now total some $10bn a year, with US PR spending increasing by 8.3 per cent this 
year according to the Holmes Report, which ranks PR firms.<br>
<br>
There is no doubt that as technology is changing the media landscape, the world 
has also become much more complex from a communications viewpoint – we have 
moved to a networked world, with our senses literally bombarded with brands, 
messages and offers. We’ve all felt overwhelmed with choice sometimes when it 
comes to accessing information, but deep down people haven’t really changed all 
that much. We all need to identify with great stories, stories which are 
relevant to our everyday life. For example, at Zeenah PR, our philosophy is very 
much to create breakthrough ideas and stories that connect and inform. People 
will always need great story telling and our clients trust us to tell them.</font></td>
