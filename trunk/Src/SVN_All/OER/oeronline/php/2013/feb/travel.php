<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt"><b>What was the big idea behind 
instituting the Travelex Oman Ambassador Awards?</b><br>
We have been operating in Oman for the last ten years and we thought that it was 
a good opportunity to reflect upon a decade of excellence. Oman was actually the 
first country in which Travelex opened its stores when it first decided to enter 
the Gulf region. We also thought that it was a good time to give back to Oman; 
and what better way of doing it, than publicising the tourism industry, which is 
a very important part of our business here.<br>
<br>
<b>Travelex is strongly associated with airport stores. Does your business 
extend beyond airports? <br>
</b>Anyone who has travelled around the world would have seen Travelex as we are 
the world’s leading foreign exchange business. We have 1,200 stores operating 
across 25 countries. We operate in 20 of the world’s largest airports processing 
2,000 customer transactions every hour. In our retail business only half of our 
stores are at airports and they are certainly more busy than our off-airport 
stores. In the Middle East, we are very much focussed on-airport and off-airport 
stores. One of the largest things that we do in off-airport stores in the region 
is to support the remittances for key workers, who are sending money back home. 
But for people who are just exchanging money our location at airports is 
important and that’s where our brand is best known around the world. We don’t 
only do walk up foreign exchange, but also have an online currency business with 
1,000 ATMs. Travelex provides outsourcing service to banks. We recently launched 
a payments business in the UK. I like to think that there are banks and money 
exchanges and there is something in between and that is us.<br>
<br>
<b>You mention about banks, money exchanges and Travelex, how would you 
differentiate the three from each other? <br>
</b>Typically banks take deposits and lend people money and one of the 
subsidiary services that they offer is foreign exchange. Their service is not 
fantastic as banks do not carry a full range of currencies and staff members in 
the bank are not very well versed with foreign currency. On the contrary if you 
go to a Travelex store you will be met with a friendly and helpful staff, who is 
well informed about foreign exchange. We always have a full range of currencies. 
Travelex has been an innovator in this space -- whether it is outsourcing for 
banks, having the biggest online business where people can buy foreign currency 
and product innovations like ATMs. We are specialists in the field. For example, 
when you come to our stores one of our colleagues will tell you that when you 
travel to Japan you would require a lot of cash, or your credit or debit card 
may not work there or certainly not work in all the ATMs. If you go to a money 
exchange house, you will not get that level of service or innovation. Travelex 
works with regulators across the world across countries helping them improve 
their foreign exchange markets. We recently spent sometime with a bank in 
Malaysia and opened our business in Malaysia. We have engaged in the process of 
how large countries rewrite and shape their foreign exchange regulations. This 
is all we do so we are knowledgeable and thoughtful about it. <br>
<br>
<b>Is Travelex a public or private company and how have you fared financially in 
year 2012?<br>
</b>Travelex is owned by funds who are advised by Apax Partners, who have a 
majority stake in the business. Lloyd Dorfman who founded the business in 1976, 
is our chairman and the second largest shareholder, with 28 per cent stake. In 
the first half of 2012, we grew our revenues on a like-to-like basis by six per 
cent globally. In rough terms the business will do a revenue worth £600-700mn of 
business every year. We employ 67,000 people across our stores globally.<br>
<br>
<b>As a result of the global financial slowdown, people have been travelling 
less as tourists and on business. Has this impacted you business negatively?<br>
</b>We had a relatively good financial crisis (GFC) as we continued growing the 
business. While it is true to say that throughout the GFC, as the Australians 
would call it, there were parts of the world which were in recession and you saw 
the impact on passengers the way you described it. One of the advantages of 
being a global business is that while one part of the globe may be slowing down, 
another part may be growing. We definitely benefitted as a result of our 
geographical diversification. One of the things that we are seeing now is that 
the world is slowing down and that is certainly a cause for some concern.<br>
<br>
<b>When you say that more people were travelling to certain parts of the world, 
are you alluding to emerging markets?<br>
</b>It’s not so easy to generalise. In Japan for example our business was very 
strong in 2012. In 2011 our business got affected by the Fukushima nuclear plant 
leak, the Tsunami and the consequent earthquake. That business has rebounded 
strongly. We invested in a business in Brazil and that business is growing 
strongly. The key is that we have not stopped investing in our business. We have 
extensive business in UAE, Bahrain and Oman. We are investing heavily in this 
region. For example, we recently opened up a new location in Barka. We are 
looking at enhancing our operations in other parts of the region too. We operate 
in India and South Africa. Travelex invests in training its people; we are 
investing in -- IT systems, refurbishing stores, new channels, ATMs, new 
products. We have recently installed an electronic payment systems in the UK, 
thus we are never short of opportunities.<br>
<br>
<b>Is Travelex trying to grow organically or is inorganic growth also a part of 
its future plans?<br>
</b>I am hoping that we can continue to grow the business organically and we 
have a very proven track record in this respect. At the same time, if there are 
sensibly priced acquisitions and opportunities then we may consider them.<br>
<br>
<b>You touched upon your operations in the GCC region. Can you share further 
details on your business in the region? <br>
</b>We have been in Oman for ten years and we have a great partner here in the 
Khimji Group. Travelex has an extensive operation in Bahrain comprising airport 
operations and off-airport stores. In the UAE we have operations in Abu Dhabi 
and Dubai as you would expect, with both airport and off-airport stores. We 
opened up an airport store in Qatar in 2012 and acquired a small business there, 
which gave us an off-airport presence. We are planning to have scale up our 
operations in Qatar.<br>
<br>
<b>How do you keep a check on financial crimes like money laundering and 
financing of illegal operations?<br>
</b>Travelex is our brand and we will not do anything that will harm our 
reputation. We take anti-money laundering customer verification extremely 
seriously. I spend a lot of time speaking to regulators around the world, like 
the US Federal Reserve and Treasury. They see us as bringing the US gold 
standard to the industry, so they welcome us entering new countries as we raise 
the entire regulatory framework. A big part of our new IT system is online 
compliance checks and customer database maintenance, which will allow us to be 
further streamline controls. It’s an area that we are very serious about. We 
follow the same standards of risk control as you would see operating in big 
financial institutions like international banks.&#8194;</font></td>
