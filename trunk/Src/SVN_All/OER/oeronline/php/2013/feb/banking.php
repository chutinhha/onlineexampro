				<b><font face="Verdana" size="4" color="#C00000">A new era of banking<br>
				</font></b><font face="Verdana" size="1">
				<br>
				</font><font face="Verdana" size="2">
				<b>first month of 2013 saw Islamic banking get off to an 
				enthusiastic start in Oman with a slew of launches and branch 
				openings. Ushering in a paradigm shift in the country’s banking 
				industry, Bank Nizwa, Oman’s first full-fledged Islamic bank, 
				commenced its operation, followed by various conventional banks 
				opening their Islamic windows. An OER report<br>
				</b></font><font face="Verdana" size="1">
				<br>
				<img border="0" src="images/banking01.jpg" width="154" height="103" align="right" hspace="3" vspace="3">Hard on the heels of the Central Bank of Oman (CBO) revising the 
				legal framework for the banking system in the Sultanate to 
				implement Islamic banking in the country, the last month has 
				seen the launch Oman’s first dedicated Islamic bank and the 
				hectic openings of Islamic windows by various conventional 
				banks. The newly introduced faith-based banking model is 
				believed to hold immense potential to take the banking industry 
				and the economy of the Sultanate to new heights.<br>
				<br>
				Bank Nizwa commenced its operation on January 10 at its 
				headquarters in Shatti al Qurum, and other two branches in Nizwa 
				and Sohar, heralding a new era of banking in Oman. The first 
				Islamic bank in the country has dedicated and highly skilled 
				staff as well as experienced industry experts at the managerial 
				level. Equipped with state-of-the-art banking systems and 
				software, the bank is capable of offering its customers the best 
				Islamic banking experience. The services offered by the bank 
				include account opening through identity cards for Omanis and 
				resident id cards for expatriates; instant issuance of 
				personalised ATM card, cheque book and much more.<br>
				<br>
				Since opening its doors, Bank Nizwa has welcomed a large number 
				of customers through its doors. “The launch of Bank Nizwa will 
				propel Islamic banking and the economy of Oman to a higher level 
				of development,” said Sayyid Amjad Mohammed Ahmed Al Busaidi, 
				chairman, Board of Directors, Bank Nizwa. “Throughout history 
				there have been defining moments which have brought about growth 
				and prosperity to our great country, Oman. It takes a lot of 
				foresight and courage to follow a dream and bring it to 
				fruition. For a long time we have aspired to fulfil the dream of 
				having a fully Sharia-compliant banking facility and we at Bank 
				Nizwa have worked tirelessly to make that dream a reality. I am 
				proud today to celebrate with you the successful opening of our 
				very own fully dedicated Islamic bank.”<br>
				<br>
				Dr Jamil El Jaroudi, CEO of the Bank said, “We take pride in the 
				fact that we are the first full-fledged Sharia-compliant bank in 
				the country with products such as current accounts, savings 
				accounts and personal finance as well as corporate business 
				services and treasury operations. Our goal is to create an 
				environment that serves as an inspiration for other Islamic 
				banks and Islamic windows at conventional banks to begin 
				operation in Oman so that the customers benefit from the best 
				practices.”<br>
				<br>
				Dr El Jaroudi added, “Through the consistent application of the 
				fair practices as laid down by the Sharia laws, we encourage 
				social fairness, profit sharing and ethical conduct. We believe 
				that these have been the pressing need of the people in Oman. 
				With our customer centric approach we wish to create new 
				benchmarks in the world of banking”.<br>
				<br>
				Speaking at the reception ceremony to commemorate the launch at 
				the bank’s head office, Dr El Jaroudi, commented: “At Bank Nizwa 
				we have an unwavering commitment to Islamic banking and we look 
				forward to assisting our customers with their financial needs. 
				The opening of our branches here at Qurum and also in Sohar and 
				Nizwa, is a significant step in the expansion of our financial 
				footprint and we will continue to invest in our growth, as we 
				further develop our presence across Oman. We will always act 
				upon the needs of our customers, delivering the Sharia-compliant 
				products and services that they require in order support their 
				current and future financial needs.”<br>
				<br>
				HH Sayyid Shihab bin Tariq al Said, Personal Advisor to His 
				Majesty, was the chief guest at the reception which was attended 
				by the founders, members of the board, executive management team 
				from the bank, media representatives and various dignitaries 
				from across the Sultanate.<br>
				<br>
				The assets of Bank Nizwa, which formally launched operations on 
				January 10, 2013, totalled over RO148mn as of end 2012. 
				Announcing the initial, unaudited financial results for the 
				quarter ended December 31, 2012, the bank also posted a net loss 
				of RO4.812mn for the period. Customer deposits amounted to 
				RO363K, shareholders’ equity was RO147.279mn. <br>
				<br>
				Operating income for the quarter was at RO8K, while the 
				operating expenditure was RO898K. In a disclosure notification 
				to the Capital Market Authority, the bank said it was announcing 
				its initial results for the quarter ended December 31, 2012 in 
				light of the fact that its ‘soft’ opening took place on December 
				23, 2012, during the period under review.<br>
				<br>
				However, Al Izz Islamic Bank, the Sultanate’s second Islamic 
				bank, which listed at Muscat Securities Market in early 
				December, is yet to commence operation. Earlier, the bank had 
				elected members of its board as Taimur bin As’ad Al Said as the 
				chairman.<br>
