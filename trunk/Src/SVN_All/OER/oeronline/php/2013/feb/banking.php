                <p align="justify" style="margin: 3px 10px; "> 
				<b><font face="Verdana" size="4" color="#C00000">Islamic windows<br>
				</font><font face="Verdana" size="2">
				National Bank of Oman (NBO) which launched its Sharia-compliant 
				banking brand in Oman under the name Muzn was the first 
				conventional bank in the country to open to the public, for 
				Islamic banking.
				</font></b></p>
				<p align="center" style="margin: 3px 10px; "> 
				<img border="0" src="images/banking02.jpg" width="525" height="352" vspace="5"></p>
				<p align="justify" style="margin: 3px 10px; "> 
				<font face="Verdana" size="1">
				<br>
				Muzn is now officially open to welcome and serve customers at 
				its flagship branch in Al Sahwa Towers - Azaiba North, which is 
				a full-fledged Islamic banking branch. An impressive soft 
				opening was held at the Muzn branch under the auspices of Sheikh 
				Khalfan Al Esry, Muzn sharia supervisory board member, Sayyida 
				Rawan Al Said, NBO’s board member and Salaam Al Shaksy, NBO’s 
				chief executive officer, in the presence of the bank’s senior 
				management and staff. <br>
				<br>
				Muzn Islamic banking branch has qualified staff and robust 
				systems in place to offer a broad spectrum of Sharia-compliant 
				products and services, an NBO official said. Muzn’s range of 
				products will cater to institutional, corporate and retail 
				customers and include products like Murabaha Auto Finance; Ijara 
				Home Finance; Sale and Lease Back Finance; Muzn Current Account 
				(Qard Hassan); Muzn Savings Account (Mudaraba); Wakala 
				Investment; Flexi Wakala Account; Murabaha Working Capital 
				Finance; Ijara Muntahia Bittamleek Term Finance and Trade 
				Finance. <br>
				<br>
				The name Muzn is the Arabic word for clouds which are, in turn, 
				associated with favourable outcomes such as growth and 
				prosperity, fortune and plenty and blessings and longevity. In 
				turn, Muzn aims to deliver growth for money, hope and 
				prosperity.<br>
				<br>
				Meethaq from Bank Muscat is all set to redefine Islamic banking 
				with the launch of operations at its dedicated branch in Al 
				Ghubra. HE Sheikh AbdulMalik bin Abdullah Al Khalili, Minister 
				of Justice, was the guest of honour at the grand ceremony. <br>
				<br>
				According to a release by the bank, “Bank Muscat is the first 
				commercial bank in Oman to receive CBO approval and begin 
				operations through independent Islamic banking window as per the 
				requirements of Islamic banking regulatory framework of CBO. 
				Ever since the announcement of the final CBO approval to begin 
				soft operations, customers have been pouring into Meethaq branch 
				to open accounts and avail various services.” <br>
				<br>
				Meethaq offers a full suite of regulated Islamic banking 
				products. The bank has invested in staff, systems and controls 
				to ensure the service is delivered in a professional, segregated 
				and fully compliant manner. Inspired by faith and a tradition of 
				trust, the Sharia-compliant products and services of the bank 
				include savings account, current account, home finance, auto 
				finance, and many other products.<br>
				<br>
				Meethaq plans to open branches in all major cities and towns 
				across Oman this year. Over a year ago, Bank Muscat announced 
				the decision of its board of directors to set up an independent 
				Islamic banking window with RO150mn capital and launch Islamic 
				banking operations in accordance with the injunctions of Sharia. 
				The final capital allocation will be determined by business 
				opportunities in the market and the board has indicated that the 
				assigned amount may increase if required.<br>
				Ahlibank has recently launched its Al Hilal Islamic Banking 
				services at its five branches (Samail, Bahla, Rustaq, Saham and 
				Muscat) simultaneously. The sixth branch is expected to open in 
				Salalah in February.<br>
				<br>
				Commenting on the launch, Hamdan bin Ali bin Nasser al Hinai, 
				chairman of Ahlibank said, “Islamic finance, by its very nature, 
				is a financial system that highlights the importance of aligning 
				the objectives of businesses with the needs of the community to 
				ensure that the overall well-being of society is not forgotten 
				when pursuing investment opportunities.” <br>
				<br>
				Abdul Aziz al Balushi, CEO Ahlibank said, “Our focus during the 
				first year will be to present the true identity and build a 
				bridge of trust and confidence with customers. This will 
				undoubtedly give us the required momentum and thrust to win the 
				customers’ confidence. Our services, products and dealing are 
				built on the tenants of truth and transparency and are in 
				accordance with Islamic Sharia principles to insure that we as 
				an organisation are committed to providing the best products 
				that the market has to offer’.</font></p>
