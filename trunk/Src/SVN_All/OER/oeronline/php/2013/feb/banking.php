				<b><font face="Verdana" size="4" color="#C00000">More players to 
				come <br>
				</font></b><font face="Verdana" size="2">
				<b>Oman Arab Bank has also launched an independent Islamic 
				banking window under the brand name “Al Yusr”. The bank will 
				launch its Islamic operations with five branches across the 
				major cities of the Sultanate during this year, said Abdul Kader 
				Askalan, CEO, Oman Arab Bank.<br>
				</b></font><font face="Verdana" size="1">
				<br>
				Further, in line with CBO requirements, OAB also introduced a 
				three-member Shariah Advisory Board to ensure the Al Yusr 
				operations are in compliance with Shariah (Islamic law). The OAB 
				Shariah board members are Dr Khalid Salim al Siyabi, Dr Ahmed 
				Subhi Ayyadi and Dr Essam Khalaf al Enezi.<br>
				<br>
				“We are confident that Al Yusr will provide customers with an 
				avenue to be true to their beliefs while offering all the 
				advantages of world class banking,” said Abdul Kader Askalan, 
				CEO of Oman Arab Bank. “Islamic banking seeks to promote the 
				ideals of a prosperous society and stable economy, based on 
				basic Islamic ideologies. Oman already has a flourishing banking 
				sector and the opening of the Islamic segment will only serve to 
				make it more diverse,” he added.<br>
				<br>
				BankDhofar is all set to announce the opening of its Maisarah 
				Islamic banking branch in Azaiba. According to a release from 
				the bank, the Maisarah has a team of highly skilled Islamic 
				banking specialists. Utilising the most advanced technology to 
				ensure Maisarah operates smoothly, they will offer a complete 
				suite of Sharia-compliant Islamic services that focus on Islamic 
				principles. The team has been busy undergoing extensive training 
				on the product portfolio, which includes savings accounts, car 
				loans and other facilities.<br>
				<br>
				CEO BankDhofar, Tony Mahoney said, “We’re really excited about 
				the opening of our first of many Maisarah branches and we’re on 
				track to see it open in the coming weeks. It’s a pretty 
				electrifying time for BankDhofar and banking in Oman.”<br>
				<br>
				Sohar Islamic, the newly introduced brand of Bank Sohar’s 
				soon-to-be-launched Islamic banking products and services, has 
				already unveiled its new logo. Bank Sohar signed an agreement 
				with Dar Al Sharia, the Dubai-based subsidiary of Dubai Islamic 
				Bank – the world’s first Islamic financial institution. The pact 
				tasked Dar Al Sharia with supporting Bank Sohar in its 
				aspirations to emerge as a frontrunner in the provision of 
				Islamic banking services in Oman. <br>
				<br>
				In addition, Bank Sohar signed up Path Solutions, a global 
				provider of software solutions to the Islamic financial services 
				industry, for the deployment of its pioneering iMAL Islamic 
				banking system.&#8194;<br>
