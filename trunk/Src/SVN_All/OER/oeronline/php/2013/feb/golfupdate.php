is the first to kick off the social calendar and is the only one of its kind in 
Oman’s corporate radar which gives industry captains the opportunity to network 
and compete on the greens and win exciting prizes.<br>
<br>
The event attracts the cream of over 250 Omani corporate heads including 
business leaders, corporate head honchos and top managers of Oman’s businesses 
for a day of high-powered golf and networking at Al Mouj Golf, The Wave Muscat’s 
prestigious golf property.<br>
