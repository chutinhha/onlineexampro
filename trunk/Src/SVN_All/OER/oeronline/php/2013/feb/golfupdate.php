Gardens, a fitting venue to shed your inhibitions and take part with gusto in 
the celebrations.<br>
<br>
Nawras, which is committed to engaging the Omani community through a host of 
different activities, views OER CEO Golf as yet another opportunity to ignite a 
passion for golf, which is fast developing as a major sport in Oman. OER CEO 
Golf provides an opportunity for Nawras Business to showcase its turbocharged 
network to the decision makers at various corporate levels.<br>
<br>
