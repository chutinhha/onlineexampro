					we will spend on the infrastructure. As a result of this, 
					when the government spends, it’s the private sector which is 
					going to get the benefits. So it is then important that the 
					private sector also plays its role in the overall 
					development of the nation. I think that time has to come for 
					people to expect reasonable profits. Its time everybody woke 
					up to the fact that there is no cheap labour any longer. 
					Start concentrating on productivity and efficiency rather 
					than on salaries. Start improving the skill sets of people 
					as even the government is concentrating its efforts on 
					education and training,” Gupte adds.<br>
