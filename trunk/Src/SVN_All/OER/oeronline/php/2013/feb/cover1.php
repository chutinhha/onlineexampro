					<font color="#333333" size="2"><b>Training and jobs<br>
					</b></font>
					<img border="0" src="images/cover05.jpg" align="right" hspace="4" vspace="4" width="200" height="176">The budget addresses the concerns in the country 
					with key emphasis on infrastructure development and creation 
					of jobs to further stimulate the economy. On the expenditure 
					side, financial allocations for Government ministries and 
					development programmes have been raised by 30 per cent to 
					complete infrastructure projects underway like ports, 
					airports, roads, water, sewage network and projects at Al 
					Duqm Special Economic Zone.<br>
					<br>
					The expenditure also includes the allocation for training 
					16,000 new jobs. Last year the government created 36,000 
					jobs for Omanis by spending RO300mn. This year, 56,000 jobs 
					would be created, of which 20,000 will be in the government 
					sector.<br>
					<br>
					“The budget is very positive and well-balanced. And the 
					additional job opportunities among youth will boost spending 
					and thereby aid the growth in credit through an increased 
					demand for finance this year,” says Ajit Engineer, CEO, 
					Muscat Finance.<br>
					<br>
					“The budget focuses on attracting local and foreign 
					investment in the private sector, supporting growth of the 
					small and medium enterprise (SME) sector, and setting up new 
					industrial zones. The increased spending on development 
					projects will give a major boost to demand for equipment and 
					working capital financing. These projects would also benefit 
					the subcontractors &amp; SMEs,” adds Engineer.</td>
