					economist to say that government’s austerity measures and 
					spending cuts lead to unemployment and lower GDP growth, if 
					not stagnation,” avers Ganesh. When the private sector is 
					hesitant to spend and has gone into a shell, governments 
					across the world needs to spend to stimulate the economy. I 
					think this is where the Euro zone is faltering. In this 
					context, the Sultanate’s deficit budget 2013 is a bold move 
					in the right direction.”<br>
					<br>
					“The projected government spending will definitely stimulate 
					the economy and create more employment opportunities. 
					Infrastructure development needs to precede any economic 
