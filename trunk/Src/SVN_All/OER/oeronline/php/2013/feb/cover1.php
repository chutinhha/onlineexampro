					The various infrastructure projects announced will lead to 
					further unification of the GCC markets which could result in 
					increased flow of foreign direct investment (FDI) into the 
					country. Considering the demographic profile of Oman, in my 
					view, education should get precedence over health in budget 
					allocations, Ganesh points out.</td>
