					<b><font face="Verdana" size="4" color="#C00000">A befitting opening
</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Royal Opera House Muscat launches the new season with Rossini's masterpiece 'Barber of Seville' 

</font></b><p align="center" style="margin: 3px 10px"> 
					<p align="center" style="margin: 3px 10px"> <br />
					<img border="0" src="/images/oct/Leisure/1.jpg">
					<p align="justify" class="style13">

<br /><br />
Royal Opera House Muscat (ROHM) launched the 2013 – 2014 season on 14 September by performing Rossini's wonderful Opera 'Barber of Seville' performed by the celebrated Teatro San Carlo from Naples. This masterpiece opera is one of the most popular operas in the world which is unique in its ability to delight the audience for 200 years. It was led by the maestro Sebastian Lang-Lessing, with the contribution of international artists like Juan Francisco Gatell, Mezzo Soprano 'Geraldine Chauvet, Filippo Polinelli and Simone Alberghini who played the role of the famous Figaro.
<br />
<br />
One hour before the performance Dr. Nasser al Taee gave an introductory lecture about the sparkling comic opera 'The Barber of Seville' and its artistic value and the uniqueness of the musical frame of the writer. The comic opera was a theatrical work written by French playwright Pierre Beaumarchais in 1775 and then transferred to an operatic performance in 1782 by the composer Giovanni Paisiello. Though the work of Paisiello triumphed for a time, only Rossini's version has stood the test of time and continues to be a mainstay of operatic repertoire and provided memorable music used in many visual and audio clips.
<br />
<br />
This season started this September continues till May next year and witnesses forty four performances from different parts of the world. The audience will be treated to an exceptional selections of Operas, musical performances, classical, arabesque, family shows, and folkloric dancing performances.
<br />
<br />
<b><font face="Verdana" size="4" color="#C00000">Triumph of the arts
</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">NPA Events' 'Dear Liar' featuring Naseeruddin Shah and Ratna Pathak-Shah wins appreciation 

</font></b><p align="center" style="margin: 3px 10px"> 
					<p align="center" style="margin: 3px 10px"> <br />
					<img border="0" src="/images/oct/Leisure/2.jpg">
					<p align="justify" class="style13">

<br /><br />
Noted Indian actors and theatre personalities Naseeruddin Shah and Ratna Pathak-Shah received a standing ovation for the NPA organised English play 'Dear Liar' at the Oman Auditorium, Al Bustan Palace, A Ritz-Carlton Hotel on September 20, which was presented by National Bank of Oman (NBO). In his welcome speech, the Indian Ambassador to Oman, HE J S Mukul said, "Theatre is the purest form of the entertainment" and lauded the efforts of NPA Events in continuing to bring quality theatre to Oman.
<br />
<br />
Naseeruddin Shah and Ratna Pathak-Shah, playing the roles of George Bernard Shaw, one of the world's greatest playwrights and Patrick Stella Campbell, a noted English actress, respectively, took the audience to a different era. The actors recreated the 40 year relationship through the hundreds of letters they sent to one another over the years, from 1899 to 1939. In keeping with the theme of the play, invitations and tickets were also sent out in a letter format by NPA Events, in their own inimitable style.
<br />
<br />
Mohammed Mahfoodh Al Ardhi, deputy chairman, NBO said, "On behalf of NBO, I would like to congratulate Naseeruddin Shah and Ratna Pathak-Shah on this wonderful production. It was a thoroughly enjoyable evening, showcasing a delightful story, brilliantly played by a talented cast. We are proud to be directly involved in bringing 'Dear Liar' to Muscat and I can certainly endorse the positive reviews which it has enjoyed elsewhere in the world." In conclusion, Ashok Suvarna, executive director, NPA Events, said, "It has always been our endeavour to bring quality theatre to Oman since nearly a decade. Naseeruddin Shah and Ratna Pathak-Shah thoroughly enjoyed performing in 'Dear Liar', as much as an appreciative audience enjoyed watching."</p>


