						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">Preserving Value</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Lean management expert, Peter Willats will be conducting a seminar on October 21, 2013 at the Grand Hyatt Muscat. In an interview to OER, he talks about the potential of Lean as a holistic management philosophy aimed at improving productivity and developing capabilities

</font></b><p align="center" style="margin: 3px 10px"> 
					<p align="center" style="margin: 3px 10px"> <br />
					<img border="0" src="/images/oct/Management/1.jpg">
					<p align="justify" class="style13">
<b>
What is Lean management all about?
</b><br /><br />
Lean management is an approach that seeks to engage everybody in the organisation to apply active problem solving to deliver superior performance by continuously improving processes and developing people's capability.
<br />
<br />
<b>Does it denote manpower reduction?</b>
<br /><br />
Lean management always looks to reduce unnecessary effort to give customers better value. As everybody's active involvement is part of Lean, and it is based on respect for people, involuntary redundancies are not part of Lean.

<br />
<br />
<b>
Can you elaborate on the origin of the concept? Is it the same as Toyota Management System?
</b><br /><br />
Lean is the term given by American researchers to describe the practices that they found when studying the Japanese automotive industry. Toyota is certainly the most accomplished practitioner of Lean. Since people have understood the principles of Lean that are the underpinnings of the Toyota system, it has been applied to many industries including finance, service, healthcare and public service.
<br />
<br />
<b> 
Oman has a very nascent manufacturing sector, especially in non oil and gas areas. Is it premature to go for such sophisticated tools?
</b><br /><br />
Lean is based on human behaviour and is, in fact, very simple. Problem solving, improving processes, dedicating to serve customers are all activities that can be applied to any organisation at whatever stage of development.
<br />
<br />
<b>
Does adoption of Lean management require high level of sophistication in terms of technology, workforce education and skill levels, etc?
</b><br /><br />
Lean management does not depend on any kind of technology. The problem solving approach is sometimes characterised as using 'the scientific method'. That does not mean that each and every staff member has to be a trained scientist. It does, however, depend on people being able to observe, describe, formulate a hypothesis, measure with facts and data and verify the effect of changes. But everyone can contribute in their own way to the current extent of their capability.
<br />
<br />
<b> 
Does this involve huge investment in time, money and efforts? 
</b><br /><br />
Lean involves a huge investment of time and effort. Spending money to solve problems is the sign of laziness. But huge amounts of time and effort are spent in every organisation doing the wrong things, correcting errors, having meetings that are incorrectly prepared etc.<br />
<br />
<b>
At what scale of operations do such techniques become relevant and useful? 
</b><br /><br />
The best Lean efforts that I have seen have been in small, owner -operated businesses. The connection to the customer and the human relations are the most immediate in small organisations.
<br/>
<br/>
<b>
There is a general confusion of Lean being merely a productivity improvement tool? Is this correct? 
</b><br /><br />
It is correct that there is confusion. Lean is about organising to give customers what they want. That may be around quality, throughput or turnaround time or about cost. So removing waste, or non-value added operations from a process improves productivity and reduces direct cost to that product, but Lean is not just about improving productivity 
<br />
<br />
<b> 
How different is Lean from normal productivity improvement techniques? 
</b><br /><br />
Most productivity improvement activities fall into one of two groups: either replacing labour with capital or cutting costs. Lean does not seek to improve things by spending money on equipment or IT. Cost cutting is done by people who do not understand the processes for which they are responsible and often leads to problems in quality. Lean primarily cuts waste not cost.
<br />
<br />
<b>
Some companies who have gone for Lean find that in the short term, quality suffers. Is this true?
</b><br /><br />
Sometimes when companies begin to measure quality carefully, they detect more defects. This may look like quality suffering but in reality customers are better off as fewer defects are passed on to them and the company has a more urgent need to improve. 
<br />
<br />
<b> 
What is the level of resistance to the introduction of Lean and how can it be dealt with? 
</b><br /><br />
There can be many reasons to resist the idea of Lean if it is incorrectly presented, such as job losses, change, transparency, extra mental effort. These are some of the common fears. Applying the principle of 'respect for people' to every planned action helps to avoid them.<br />
<br />
<b>
What are the key obstacles to the introduction of Lean in a manufacturing organisation?</b>
<br /><br />
Departmental thinking, inappropriate investments from the past, payment by parts produced, standard cost accounting, senior managers who think they know best.
<br/>
<br/>
<b>What are the minimum prerequisites for an organisation to successfully embark on a Lean journey?</b> 
</b><br /><br />
There has to be a clear need to change. Top management need to address their own behavior first. That is the only commitment that really counts.
<br/>
<br/>
<b>
To get the buy-in of the workforce, should there be incentive schemes designed around Lean implementation? 
</b><br /><br />
Other than changing schemes that promote the wrong behaviour such as standard costing, piecework payment, nothing much needs to be introduced. The feeling of being valued in an organisation dedicated to developing people and serving customers better is its own reward. The company will be stronger and more competitive and thereby jobs will be more secure.
<br />
<br />
<b> 
Where does TPM (Total Productive Maintenance) fit in the Lean scheme? 
</b><br /><br />
TPM, the practice of mass involvement of far-reaching maintenance and reliability systems, is particularly important in situations where the process depends on the performance of complex equipment. It is based on the same principles as Lean.
<br />
<br />
<b>
What should be the measures for gauging the success of Lean management?
</b><br /><br />
Meeting customer requirements with the minimum of effort and resource while involving and developing everyone. There are lots of technical ways to measure the components of this such as throughput time, on-time in full, defect rates, warrantee costs, suggestions per person, but the principle is the important thing to remember.

