					<b><font face="Verdana" size="4" color="#C00000">A step change</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Sandeep Sehgal, Chief Executive, United Media Services talks about the company's tie-up with Bloomberg Businessweek for bringing out the latter's Middle East Edition and what lies in store for readers and advertisers. Mayank Singh reports<br />
						
</font></b><p align="center" style="margin: 3px 10px"> 
					<p align="center" style="margin: 3px 10px"> 
					<img border="0" src="/images/oct/media/1.jpg" >
					<p align="justify" class="style13">
<br />
<br />
<b>
Bloomberg Businessweek and UMS have partnered to bring out the Middle East Edition of BBW. Can you give us details of the process that went into winning this franchise and the kind of competition that UMS was pitted against?
</b><br /><br />
After establishing a strong presence in key GCC markets, UMS was looking at binding this collective strength for taking on opportunities at the regional level. It had tasted success in bringing New York Times style magazine 'T' to the region, anchoring it in the most prominent markets as T-Qatar and T-Emirates. Now it was on the lookout for a global brand that had strong regional relevance and Bloomberg Businessweek (BBW) was a perfect fit. Globally Businessweek, an 80-year old brand, has gone through a complete revamp after getting into the Bloomberg fold and being reborn as BBW. 
<br />
<br />
As a part of its strategic approach to capture the Middle East Region, BBW was looking for a partner to do not just its Arabic edition but considering its core readership of business leaders, an English language edition as well. Bloomberg bought into our recommendation of launching the English edition followed by the Arabic edition. Over a one year period and multiple meetings and presentations in New York, and pitching against strong regional publishers UMS won the pitch standing tall on its credentials in each of the core GCC markets. 
<br />
<br />
<b>Is BBW going to be a part of UMS Oman or are you looking at alternative arrangements for the magazine?</b>
<br /><br />
UMS group has established an independent company - UMS International FZ LLC operating out of Dubai Media City, which apart from having its dedicated resource base, will be well supported by the the regional network of UMS. 

<br />
<br />
<b>
When is the first edition of the BBW Middle East expected to hit the stands and what can readers expect from it?
</b><br /><br />
The magazine is being launched as a fortnightly and the first edition will hit the stands on October 1, 2013. The first issue sets the benchmark for us moving forward. It features a great mix of regional and global content sourced from our dedicated team of reporters and editors as well as the Bloomberg's strong journalist base across the region. BBW is known for its cutting edge approach to business reporting which will now make the Middle East reader feel at home. 
<br />
<br />
It is unlike any other business magazine in this part of the world and we are confident that it will have a huge impact on the market.
<br />
<br />
<b> 
What kind of a circulation and geographical spread will BBW Middle East have in the region?
</b><br /><br />
To begin with BBW Middle East will have a print run of 25,000 copies covering key markets in Middle East and North Africa. 
<br />
<br />
<b>
The International edition of BBW and other magazines likes Forbes, Fortune etc have a presence in the region. Given this fact, what is going to the BBW Middle East's differentiation in the market vis-a-vis competition?
</b><br /><br />
Apart from the editorial strength, BBW is known for its vibrant design, and a design that brings the entire offering alive to its readers. The look and feel of BBW's Middle East edition delivers the essence of the global brand with a strong regional component. The content of the Middle East edition is sourced as per its relevance to the local reader. The beauty of the product is that we can reproduce the best international content and mix it with fantastically produced local content, with the same level of quality and editorial standards that BBW stands for. So the local reader gets the best of both the worlds with the Middle East edition. 
<br />
<br />
There is empirical evidence to prove that BBW subscribers take action based on promotional messages in BBW. For example, 48 per cent of subscribers who see an advertisement in BBW visit the company's website, while 37 per cent brought a product/service as a result of seeing an advertisement in the magazine. Nine out of ten subscribers pay attention to the advertising that appears in BBW. Thus one can confidently say that it is a perfect medium for brand building and action oriented advertising. Secondly, the presentation of the magazine is so captivating, that it leads to reader stickiness increasing visibility for advertisers. 
<br />
<br />
<b> 
This marks a first for an Oman based media house. How significant is this partnership for UMS and in what ways will this enrich your offerings in the local market?
</b><br /><br />
It is matter of great pride that an Omani company has been able to attract two of the world's top publishing names – New York Times and Bloomberg Businessweek. It is an assertion of UMS being a strong regional player well anchored in the key markets of the region. This partnership presents UMS with an opportunity to have access to a vast pool of well researched content besides enhancing its editorial best practices. We are proud to put Oman on the world map. Overall, UMS is moving towards achieving its corporate vision of establishing a global footprint that will deliver long term sustainable growth.<br />
