						<span class="style27"><strong>Nominations open for Dossier Construction Awards & Summit 2013</strong></span><p align="left" class="style13" style="line-height: 17px"><br />
                        
						<font size="1" face="Verdana">
						<br /><p align="justify" style="margin: 3px 10px" dir="ltr">Under the auspices of the Ministry of Transport & Communications, the Dossier Construction Awards & Summit 2013 will be organised on November 4, 2013 at Intercontinental Muscat. Targeted at the construction, infrastructure and real estate industries in Oman, the first-of-its-kind event will felicitate industry performers, best practices, technologies and project excellence through an awards ceremony; and drive the exchange of knowledge within the industry through an intellectual forum in the form of a summit.
						<br />
						<br />
HE Ahmed bin Mohammed bin Salim al Futaisi, Minister of Transport & Communications, will be the chief guest at Dossier Construction Awards & Summit 2013. The leading industry players are supporting the prestigious event that is being organised under the patronage of the Ministry of Transport & Communications. Atkins is the strategic partner; KPMG is the validation partner and; Muscat Municipality, Oman Society of Engineers and GUtech are the support partners. Muscat Hills Residences Phase-2 is the gold sponsor whereas International Heavy Equipment (IHE), Hasan Juma Backer Trading & Contracting, Voltamp Energy, National Bank of Oman (NBO), Oman Cement Company, Teejan Group of Companies and Al Habib Real Estate are the silver sponsors of the event. The event is instituted by Dossier-Construction magazine, Oman's first and only business-to-business (B2B) magazine on the construction sector, published by United Media Services.
						
						<br />
						<br />
Dossier Construction Awards & Summit 2013 will be attended by the key ministers and senior officials in the Government, senior-to-mid level executives from the public and private sectors, and leaders of relevant industry bodies. The first half session dedicated to the summit will bring about impactful and compelling educational sessions from senior-level international and local leaders of consulting, construction and real estate companies who bring to the forefront challenging issues and new insights into the business opportunities in today's construction market.
						<br />
						<br />
In a gala function in the evening, the best performers from the industry will be presented with the coveted trophy in 16 different award categories. The winners of the awards will be selected by an independent panel of eminent industry experts; the selection process will be validated by KPMG. Interested companies and individuals could nominate themselves for the awards on www.dossieroman.com before October 16. For more information, contact Fidha on +968 99653581 or fidha@umsoman.com.
