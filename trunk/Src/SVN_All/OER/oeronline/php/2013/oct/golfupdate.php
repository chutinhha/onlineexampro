					<b><font face="Verdana" size="4" color="#C00000">Breaking new ground</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The National Bank of Oman Golf Classic to be held at Almouj Golf 
from October 24 to 27, 2013 will be the first European Challenge Tour event to be played in the GCC region
</font></b>
                        <br />
                        <br />
						<center><img src="/images/oct/Golfupdate/1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">For the first time Omani golf fans would be able to see the future stars of The European Tour in action at close quarters, as Almouj Golf hosts the National Bank of Oman Golf Classic from October 24 to 27. "We are proud and honoured to be the title sponsor of the first professional golf tournament to be held in Oman and to be breaking new ground in hosting the first European Challenge Tour event to be played in the GCC," said Mohammed Mahfoudh Al Ardhi, deputy chairman of National Bank of Oman. "The National Bank of Oman Golf Classic will bring many benefits to the Sultanate, particularly among the youth and the business sectors. Golf has really taken off in Oman in a very short space of time and our youngsters have become enthusiastic golfers." 

<br />
<br />
The tournament will help boost the interest of youngsters and give the Omani national team players, like Azaan Al Rumhy and Ali Hameed, the chance to compete against European Challenge Tour professionals at a higher international level. Playing any sport against stronger opposition is the ideal way to improve standard and it will be interesting to see how the Arab National amateurs will fare against the future stars of this global game.
<br />
<br />
"We have three excellent golf clubs in Muscat and another under construction, with excellent facilities, and we are pleased to assist the growth of the game among our youngsters. They are the future of Oman," says Al Ardhi. "This is a milestone event for Oman which will create many other opportunities. The tournament not only puts the Sultanate on the world golf map but it also benefits the business and tourism sector in promoting Oman to the global corporate and investment community."
 
<br />
<br />
Tarratt, director of European Tour International Dubai Office, said, "Many of last year's Ryder Cup Team made their names on the European Challenge Tour. They include players like Martin Kaymer, Justin Rose, Ian Poulter, Nicolas Colsaerts, plus Major winners such as Louis Oosthuizen, Trevor Immelman and Michael Campbell."
<br />
<br />
The European Challenge Tour, along with Qualifying School, is the traditional route for up and coming youngsters to hone their game and, if they're good enough, to play their way onto The European Tour. This will be one of the most crucial tournaments of the Challenge Tour season and it will be fascinating to witness the stars of the future in action."
<br />
<br />
"We thank National Bank of Oman for their sponsorship of this prestigious ground-breaking tournament and for offering sponsor's invitations to the top two players in both the PAN-Arab Championship and the GCC Championship and to other Arab Nationals in the region," Tarratt added.<br />
