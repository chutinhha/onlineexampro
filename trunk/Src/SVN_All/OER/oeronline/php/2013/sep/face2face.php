<font face="Verdana" size="1">
Hani Obeid, Senior Advisor, Zubair Small Enterprises Centre speaks to Mayank Singh about the importance of SMEs and the necessity of promoting them
<br />
<br /><center><img src="images/Face2/1.jpg" border="0"></center><p  class="style10">

<b>The Zubair Small Enterprises Centre was launched in June 2013. What is the purpose and vision of the centre?
</b><br />
Zubair Small Enterprises Centre is one of the many CSR, strategic visions that have emanated from the Zubair Group of Companies. The members of the Zubair family continue their quest and dedication of giving back to Oman's society and becoming a solid contributor in the arena of corporate social responsibility. The importance of small enterprises to every society and specifically the Omani society was a driving factor that led to the creation of the Centre. The vision reflects the Group's belief about the great importance of the small enterprises/entrepreneurs in enhancing the national economy and spreading its benefits. According to various experts, from a static point of view SMEs contribute heavily to job creation, while from a dynamic point of view they are a nursery for larger firms and a starting point to greatness; many of the Omani entrepreneurs I have met, have the passion and fire within them to make their dreams a reality - they could be the next Google or Microsoft in technology or the next Honeywell in manufacturing. Overall, when you combine their static and dynamic output, we find that SMEs are very important to any society and GDP contribution and more so to developing countries such as Oman and others where there is a large percentage of educated youth, with a passion to succeed, and not by hand-me-downs but through hard work.  
<br />
<br />
<b>Give us a first-hand account of the working of the Centre?
</b><br />
On an average I meet four-to-five SME entrepreneurs everyday and each meeting lasts upto two-to- three hours. I give SMEs a sense of trust, by listening to them and giving them my undivided attention. This is something, I truly focus on and highlight to my team to carry as a trait. Furthermore, it's a characteristic that I urge all other similar organisations with the capability and capacity to do so to mimic should it not be in their service offerings. My job at the Zubair Small Enterprises Centre is to be more than a business incubator. My effort is to provide SMEs with all the knowledge that I have had over 20 plus years of my career in consulting organisations of various sizes and sectors internationally. Between myself and my team we give them all that we have. We help Omani entrepreneurs, both men and women, from established businesses to start ups in refocusing their business strategies, tweaking their marketing plans, and rethinking their financial statements. Our services have unfolded to be on a spectrum from mere advice to actually sitting down with entrepreneurs and working side-by-side on many of the elements required to take their business to the next level and on the right path. Overall, they feel a sense of management guidance and caring and that we are in this together. Apart from this there is the funding exercise. Our funding comes from The Zubair Corporation. I am usually asked, 'How much money will you give me?' and I tell these entrepreneurs, that the management has not set aside a particular amount as it would not be fair. The amount of money that an SME gets is a function of the homework that they have done and there is no easy hand me down. If they have the right business model, a strong business case, have done their due diligence, market intelligence study, and potentially have letters of intent and the right product or service, then it is difficult for any organisation or institution to walk the other way but to assist in any way possible. Starting a business is not easy but with passion, focus and drive as key elements, you are on your way to success. 
<br /><br />
We are also working on creating an ecosystem of partners, mentors, subject matter experts who can assist small enterprises. I am reaching out to various institutions to create strategic partnerships, service partnerships, consumer partnerships that can provide a spectrum of services. There are most likely Omanis and successful entrepreneurs living abroad who are looking at avenues of giving back to society and we want to be that platform for them and hence are keen to reaching out to them.
<br /><br />
<b>There have been other initiatives in the Sultanate for years which have been funding and promoting SMEs. In what way is this initiative different from the predecessors?
</b><br/>
We have a number of differentiating factors. I would say funding is one, we are committed to assisting ten entrepreneurial concepts every year, from a whole basket of applicants with direct funding. We will provide them with seed capital to get them on their feet. The other differentiation element is the day-to-day assistance that we give small enterprises. Apart from financial resources, we consult them on all facets of operations and work with them on a yearly basis for 12 months to see that they are a sustainable concept. Thus we are different from centres that provide only funding or consultation. Furthermore, we provide trainings and workshops, management templates for business creation, services offices, and access to our international network through our membership with the International Network of Small and Medium Enterprises (INSME). Finally, a distinguishing factor is the Zubair Corporation itself. We are a holding company with a presence across sectors. As a result, small entrepreneurs can direct inquiries to any of our executives as our in-house experts cut across domains like legal, marketing and finance.
<br /><br />
<b>The Zubair Small Enterprises Centre does not have a corpus or a fund to lend to entrepreneurs, so how does the funding model work?
</b><br/>
A preselected board will hear a selected number of entrepreneurs firsthand and will decide on whom to fund in consultation with the Centre's staff.
<br /><br /><b>
The Centre was launched three months back and though it is early days, how much work has been done till date?
</b>
<br />
Today, we have over 45 members who are both individual and small enterprises. We are close to short listing a set of candidates who will present their ideas to a Board. Out of these, five will be chosen for direct support and funding. Globally, we are amongst 80 plus countries that are members of INSME. Being a part of this network gives us access to various organisations like the UN, World Bank, IMF, access to research, technology, innovative ideas and best practices and it will be an opportunity for members in our centre to benefit from it. The membership gives us visibility in international networks that supports SMEs, globally. It also gives a validation to our Centre's vision and mission. It is also a passport to speak to the funding agencies that may assist us in supporting SME's.
<br />
<br />
<b>
While this is a great initiative, naysayers may question whether helping a handful of SMEs in a year is enough. How would you react to it?
</b>
<br />
From what I can see, I can surely say that this initiative is having a cascading effect. It's never enough to help SMEs. The Centre is not a project, but a programme. It is the responsibility of every individual in the Sultanate to look out for his brothers and sisters. The Zubair Corporation firmly believes that it is a step in the right direction and they are committed to sustain such an initiative over the years. I am amused by people who say that you have competition. I don't see similar initiatives as competition, if an exposure to our programme encourages others, then it is good. Even though the number 10 keeps coming up for those who we provide direct support and funding every 12 month period, our doors are open to all our Centre members from Small Enterprises who require any such assistance from consulting to business plan review to investment vehicle introductions to access to our network of partners. Having gone through our Centre membership process gives all small enterprises key validation elements to take their business concepts to the next level in a business maturity cycle. Our number members increase every day and does not stop. This is an indication that what we offer remains appreciated. We are not going to stop at that and we are open to assisting all our members as what makes me wake up every morning wanting to go to work is the fact that I am meeting another ambitious entrepreneur asking for our Centre's help and I am more than willing to listen and advise as often as he/she /they want(s) it. As our motto at the Centre stands today: Build Confidence, Get consulted – Get Funded – Launch – Accelerate Your Business.

