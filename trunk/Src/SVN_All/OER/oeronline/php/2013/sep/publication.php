					<b><font face="Verdana" size="4" color="#C00000">HE Futaisi unveils Progress 2013-2014</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font>
						<br />
						
						<center><img src="images/Publication/1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">HE Dr Ahmed bin Mohammed al Futaisi, Minister of Transport, has unveiled Progress 2013-2014, the 19th edition of the annual bilingual country book from United Media Services. Also present on the occasion were SandeepSehgal, chief executive, UMS; Alpana Roy, executive vice president, UMS and members of the editorial team. 
 
<br />
<br />
HE al Futaisi expressed his delight in launching the book which salutes His Majesty Sultan Qaboos Bin Said on the occasion of the 43rd anniversary of Oman's Blessed Renaissance. He said the publication will be a comprehensive reference work shedding light on all areas of development in Oman under His Majeasty Sultan Qaboos bin Said and chronicling major national achievements. He said the book takes an in-depth look at the various sectors that have contributed to the Sultanate's development and modernisation, highlighting government's focus on SME development and the policy initiatives to encourage the youth to do their best for Oman's economic development and inclusive growth.
<br />
<br />
This year's Progress stands out for its exhaustive analysis of various sectors such as oil & gas, banking, NBFCs and insurance, capital markets, education, healthcare, agriculture, retailing, aviation, telecom, information and communication technology, real estate, manufacturing, power, infrastructure, tourism etc. Each of these sectors is covered in dedicated chapters highlighting the strides taken by the Sultanate in these areas.
<br />
<br />
 The opening article looks into how SMEs can serve as incubators for job creation in the Sultanate, examining the government policies directed towards facilitating job creation and for creating an atmosphere conducive for developing small and medium industries, as well as tiny units to generate revenue for the young people who enter the job market every year. Since SME development is vital for generating employment opportunities for the youth, the government has announced a series of measures to ensure a faster growth of the sector. A sizable portion of the 56,000 jobs that the government is expected to create in 2013 will be in the SME sector.
<br />
<br />
Progress, the first annual country book in the Sultanate to go online, is available in a digital version on www.progressoman.com. The digital version offers readers online access to a more comprehensive range of articles than the print edition. It will serve as a one-stop reference point for people seeking information on Oman's remarkable journey over the last 43 years.
