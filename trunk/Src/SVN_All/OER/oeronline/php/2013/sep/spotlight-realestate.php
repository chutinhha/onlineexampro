					<b><font face="Verdana" size="4" color="#C00000">Tapping the potential</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">There is still a shortage of affordable houses as many real estate developments are focusing on either integrated tourism complexes or luxury villas

</font></b>
						<br /><br />
						
						<center><img src="images/Spotlight/1.jpg" border="0"></center>
                        
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">
 
Oman's housing sector still lacks ample supply of affordable housing units, primarily due to a dearth of large involvement of private developers to build relatively cheap houses or apartments. Unlike many other countries, the Sultanate's affordable housing sector is primarily supported by government agencies. The Ministry of Housing provides housing units for low income families through the housing assistance programme, the housing loan programme and the housing units project. The eighth five-year plan allocated RO360mn for housing assistance programme, which is aimed at providing houses to 18,000 families. Despite all these efforts, there is still a shortage of affordable houses as many real estate developments are focusing on either integrated tourism complexes or luxury villas.
<br />
<br />
Although government's recent decision to raise the corpus fund for housing to RO100mn for another three years enabling more locals to own houses, it is not enough as there is a long list of 50,000 people waiting for interest-free loan from the government agencies. More private sector involvement is needed to make it available to a large chunk of the population. To resolve this problem, Oman Real Estate Association has recently asked the government to allot land to private developers to build affordable apartments as land price in Muscat is exorbitant. Oman needs at least 25,000 housing units per annum in the coming years, which is mainly driven by newly married couples seeking separate houses. According to a rough estimate, 30,000 new couples in the Sultanate need housing every year.
<br />
<br />
<center><img src="images/Spotlight/2.jpg"></center>
<br />
<p align="justify" class="style13">
<b>Premium projects</b>
<br />
<br />
The development of premium projects is not targetting those who seek low-price houses due to unaffordability. In fact, several real estate developers, including Saraya Bander Jissah, The Wave, Muriya and Alargan have massive development plans in different parts of the country. The integrated tourism complex Saraya Bandar Jissah offers exclusive homes and two five-star beachfront hotels, as well as recreational and community facilities, on a beach surrounded by the Hajjar Mountains, just east of Muscat. With villas, apartments and hotels, the development is positioned sympathetically amongst 2.2 million square metres of undulating hillsides. 
<br />
<br />
The project is being developed by Oman's tourism development and investment arm, Omran, in partnership with Saraya Oman, a subsidiary of Saraya Holdings, the leading international development company.
<br />
<br />
Muriya, a joint venture between Egypt's Orascom Development Holding and Omran, is also developing two major integrated tourism complexes - Jebel Sifah and Salalah Beach. These two projects are part of integrated tourism complexes, which allow 100 per cent foreign ownership. Spreading over an area of 6.2 million square metres, Jebel Sifah is home to luxurious freehold apartments and villas of diverse architecture and style, four five-star hotels and two marina boutique hotels, up to 200 berth inland marina and marina town and retail venues. Likewise, Salalah Beach is spread over an area of 15.6 million square metres with 8.2-km beach front. This integrated tourism complex will comprise of high-end luxury freehold apartments and villas, its own shopping and retail outlets, five five-star hotels and two marina boutique hotels and several other luxury amenities. Another premium project coming up in Muscat is Qurm Hills. Alargan International Real Estate Company, the Kuwait-based Real Estate developer, has launched this project, which comprises of 109 plots. The total size of the Qurm Hills project spans 164,900 sq meters with a built up area is 119,000 sq meters. 
<br />
<br />
In yet another development, Al Khonji Real Estate & Development, is building their latest real estate project Rimal. Once completed, Rimal will be one of the largest of its kind in Muscat covering 50,000 sqm comprising 254 apartments, high-end leisure facilities such as two private gyms, a choice of two swimming pools for children and adults, a shopping mall, climate controlled corridors and three levels of underground parking – making Rimal one of the most convenient places to live in Muscat. The Wave Muscat has also announced massive expansion programmes, which are expected to enhance availability of luxury homes. The Wave Muscat started construction of its Siraj and Reehan Gardens, which boasts a collective 143 apartments and villas at Oman's leading integrated tourism complex.
<br />
<br />
After completing the first phase of Tilal complex, which mainly comprises the Muscat Grand Mall, residential apartments and office space, Al Madina Real Estate has started work on the second phase. The project is the largest mixed use development in Oman, combining a central place to live, a large shopping mall offering a host of regional and international brands, professional office space with the latest facilities, a health club, hotel apartments and underground parking – all spanning 250,000 square meters. The second phase will include a five-star business hotel with 300 rooms and forty luxury apartments, while in the third phase, plans are afoot to expand the Muscat Grand Mall. 
<br />
<br />
In fact, rents are rising in integrated tourism complexes (ITC) like Muscat Hills and The Wave, according to Cluttons. With more projects coming to the market, the ITC residential sector sector was showing increasing signs of activity. After several challenging years following the impacts of the global financial crisis, there is evidence of growing strength and confidence in the ITC residential sector, which is translating into a significant increase in developments.
