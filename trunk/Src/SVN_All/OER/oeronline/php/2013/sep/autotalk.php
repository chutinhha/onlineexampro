						A popular choice</font></b><br><br>
                        <b> The seven-seater Grand Santa Fe suits the needs of a modern family with its practicality and premiumness. An OER review</b>
                        <p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/Autotalk/1.jpg" width="400" height="306" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				Santa Fe has been one of the most-recognised SUV nameplates in the market. One can instantly feel the enhanced dimensions of the third generation all-new Grand Santa Fe. The SUV is 225mm longer in comparison to the Santa Fe, and benefits from a wheel base that has been extended by 100mm. Higher and wider too, the result is greater head and leg room for all second and third row passengers, ensuring an even more comfortable journey.<br>
				<br>
				The Grand Santa FE offers a flexible seating layout, with the standard seven-seat version being equipped with three split folding seats in row two and fully folding seats in the third row. The three-row rear boot space is 114mm longer than in the Santa Fe. In addition there is 41 per cent more boot space behind the third row of seats. In total, the Grand Santa Fe provides 634 litres of boot space behind the second row, while there is additional storage space beneath the boot floor. This is enough to pack any number of bags on a road trip or while going on a vacation.
                <br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/Autotalk/2.jpg" width="300" height="240" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
                <b>The styling</b><br><br>
				The Grand Santa Fe styling betrays an eye for detail. Based on Hyundai's 'Fluidic Sculpture' design philosophy, the SUV adopts a strong, dynamic exterior look, while taking most of its design cues from the Santa Fe. Notable differences at the front include a new, four-bar radiator grille, premium-style fog lamps and a wide skid plate. From the side there is a wider rear quarter glass, different 18 and 19 alloys and enhanced wing mirrors. At the rear, the premium look is topped off by a new dual muffler, restyled rear combination lamps and new rear bumper skid plate.<br>
                <br>
                The interior offers everything that is expected from a 'Modern Premium' offering. High quality materials are used throughout the cabin, including the option of full leather upholstery. Optional features include a 4.3 inch TFT-LCD display, smart navigation and a high-class sound system. Solar glass to protect the cabin from UV and infrared rays, rear door curtains, and an advanced climate control system with dedicated controls in the third row all combine to help beat the Middle East heat.<br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/Autotalk/3.jpg" width="300" height="188" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
				<b>Safety comes first</b><br><br>
				Hyundai has worked to ensure that the Grand Santa Fe is one of the safest vehicles in the market. Cutting-edge active safety systems available include electronic stability control, a hillstart assist control system that prevents the vehicle from rolling when on an incline, as well as a brake assist system, which increases braking pressure in an emergency situation. In addition to a traction control system, vehicle stability management is available on top-of-the-range models and promotes stable car manoeuvring when the car may be liable to skidding.<br>
				<br>
				The new model offers a Lambda II 3.3 MPI engine producing 270bhp and up to 318Nm of torque. Customers will have a choice of 2 or 4WD, and a six-speed automatic transmission. <br>
                <br>
                In a region where large, seven-seat SUVs are in high demand, the Grand Santa Fe is perfectly placed to meet most family transportation needs, all wrapped up in a premium package. The Grand Santa Fe is sure to prove to be a popular choice with families with an active lifestyle, who are looking for a SUV that combines practicality with premium quality.<br></font></td>
