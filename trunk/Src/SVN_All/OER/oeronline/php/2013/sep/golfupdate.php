					<b><font face="Verdana" size="4" color="#C00000">A big leap</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Oman is set to make its debut on the MENA Golf tour strengthening its position as golfing destination of choice</font></b>
                        <br />
                        <br />
						<center><img src="images/Golfupdate/1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">Oman will make its debut on the MENA Golf Tour when the Sultanate plays host to the Ghala Valley Open later this year, adding fresh impetus to its growing reputation as a major golf destination. 

<br />
<br />
Boasting a total prize fund of $50,000, the inaugural event at Ghala Valley Golf Club from 3 to 6 November will see a collection of international players join the field along with the region's top talent, underscoring the tour's resolve to develop and promote the game across the whole region with the aim of producing a future generation of champions. 
<br />
<br />
 Supported by the Oman Golf Association, the staging of the tournament – which will be the ninth and the penultimate stop on the MENA Golf Tour this season – provides a great opportunity for Oman to showcase its year's of investment in building golf courses that conform to the highest international standards.
 
<br />
<br />
<strong>Garnering support</strong>
<br />
<br />
"We are absolutely delighted to be a part of the MENA Golf Tour and look forward to welcoming players from around the world," said HE Mohammed bin Hamad Al Rumhy, minister of Oil and Gas. "The tour has done a great job in creating a competitive environment for the region's players to grow and excel on the international stage. I am sure the presence of the region's top players, especially, will motivate local talent to take their game even more seriously and strive for excellence," added said HE Al Rumhy, himself an avid golfer and father of Azaan Al Ruhmy, Oman's leading amateur.
<br />
<br />
Mohamed JumaBuamaim, chairman of the MENA Golf Tour, said "This will be our first visit to Oman which we see as another positive development for golf in the region. I would like to thank Ghala Valley Golf Club for hosting the event which, I am sure, will become a popular and permanent stop on the tour.
<br />
<br />
"Oman, which is famed for its striking landscape, has made great strides in putting together solid infrastructure for golf and hosting of a tour event in the Sultanate strongly endorses the country's commitment to broadening the base of golfers in the Arab world.
<br />
<br />
"Our tour is unique in the sense that it allows amateurs to compete and benefit from the experience of playing with professionals," he said. "These are exciting times for us and we look forward to working with the Oman Golf Association with the mutual goal of putting the region's golf on the world's sporting map."
<br />
<br />
Launched by the ShaikhMaktoum Golf Foundation in 2011, the MENA Golf Tour is affiliated to the R&A and the Arab Golf Federation. 
<br />
<br />
Each tournament – a minimum 54-hole stroke-play event – carries World Ranking points and $50,000 in prize money while the season-ending MENA Tour Championship has a prize fund of $75,000.
<br />
<br />
The top three professionals and the leading amateur from their respective orders of merit receive an invitation to the Omega Dubai Desert Classic, the longest-running European Tour event in the region.
