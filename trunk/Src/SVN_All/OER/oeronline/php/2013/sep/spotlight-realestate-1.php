Survey the current rents by having a comparable market analysis (CMA) in your area by looking at different properties within your area or reviewing ads placed by other landlords or calling Real Estate companies. If the rents you are charging are below what other landlords are achieving in market, increase them when the current tenant's lease expires. If the tenant vacates, you should be able to replace him with another tenant.
