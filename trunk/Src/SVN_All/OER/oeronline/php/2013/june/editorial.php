				<font face="Verdana" size="1">
				Sustainable development is about meeting the needs of today without compromising the needs of future generations. It is about improving our standard of living by protecting human health, conserving the environment, using resources efficiently and advancing long-term economic competitiveness. It requires the integration of environmental, economic and social priorities into policies and programmes. It also requires action at all levels – citizens, industry and governments. The best scientific assessments available have established a link between human activity and climate change and they should be studied closely in order to give a sound basis for raising awareness and making effective policy decisions.
 
				<br />
				<br />
 Environmental sustainability is also an essential tool in the fight against income inequality and economic disparity. Sustainability offers a huge opportunity to build a 21st century clean economy that boosts two critical imperatives: environmental protection and economic prosperity – especially for those on the lower end of the wage scale. Poor nations and sectors of society are particularly vulnerable to the adverse consequences of climate change, as they have lesser resources and capacity to mitigate the damaging effects of climate change.
Given the magnitude of the problem, no country alone can solve the problems related to our common environment. It requires nations to overcome self-interest through collective action by the adoption of a coordinated, effective and prompt international political strategy. 

				<br />
				<br />
 OER’s Oman Green Awards (OGA) initiative is a small step towards raising awareness about sustainable development. Over the last four years the platform has trained the spotlight on environment related efforts and issues in the Sultanate. We hope that the 2013 edition of OGA on June 5 will prove to be another step in this direction.
