				<b><i><font face="Verdana" size="2">Public spending increased by nearly 40 per cent to RO1,520mn in the first two months of 2013, from nearly RO1,086mn in the same period of 2012

</font></i></b><br />
				
				<font face="Verdana" size="1">By Oliver Cornock</font></p>
                </td>
