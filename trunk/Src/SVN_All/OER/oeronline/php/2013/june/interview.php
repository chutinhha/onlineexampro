					<p align="justify" class="style13">
<b>What made Sharqiyah Desalination Plant (SDC) go in for an IPO?
</b><br />


We created the company in 2007 and at that time the founder shareholders Veolia and National Power and Water Company signed an agreement with the Ministry of Finance to be listed four years after the incorporation of the company. So it was supposed to happen in 2011, but unfortunately we had Cyclone Gonu during the construction period and we had some delays. So the ministry agreed to postpone the IPO process by two years. So the aim of the contract between the founder shareholders and the Ministry of Finance is to give access to all Omani residents and citizens to safe investment in a dependable sector like water.
<br />
<br />
What is reason for restricting the IPO to only individual investors and keeping out corporate investors?
<br />

Normally IPOs are open for individuals as well as corporate customers. However since our IPO is small, just 2.28 million shares, it has been a requirement by the Capital Market Authority (CMA) to restrict it to individuals.
<br />
So the aim of the founder shareholders was to offload 35 per cent of the paid up capital which is RO6.52mn. 
<br />
<br />
<b>What is the actual project cost including capex and opex?
</b><br />
 
The project cost initially was $170mn in terms of capital expenditure and opex for the year 2012, we had a revenue of RO9.6mn with an opex of RO4mn and then some general and finance and IT charges. We had quite a high revenue stream and profit margins of around 30 per cent for the past few years. And we expect that in the coming years it will be between 18-28 per cent.

<br />
<br />
<b> How did you arrive at the share premium?
</b><br />
 The offer price is RO1.063 per share. As per the project founders agreement, this has been arrived at as per a formula. When we have a nominal value of RO1 and there are offer expenses of 20 baiza per share, the share premium is only 43 baiza per share. Basically the founder shareholders are not making any profit on this IPO. It is only for the benefit of the Omani people to make a safe investment with a discount of 40 per cent compared to the book value. So it is clearly not an operation where shareholders will make money. 
<br />
The minimum limit of subscription is 100 shares and the maximum is 10 per cent of the total offer size representing 228,205 shares.
<br />
 The IPO earnings will go to the shareholders namely Veolia, a multi-billion dollar global company, which holds 65 per cent and National Power and Water (part of the Suhail Bahwan Group) which has 35 per cent, currently.
<br />
<br />
<b>What are the financials of the company?
</b>
<br />
 The projections were done by Ernst & Young. The revenue will increase (as per the consumer price index in Oman and US) till 2015 and accordingly the output is forecasted to reach full capacity in the corresponding period. Then the cost of sales like electricity will increase as per the agreement with the O&M company which is a fixed contract for the next 20 years.
 
<br />
 The basic earnings per share is expected to be RO0.28 and RO0.41 per share over the next five years. And we forecast to distribute 90 per cent of the net profit as dividend. Till 2015, we expect to distribute a dividend of RO1 per share. So in terms of dividend, the yield is very high and you will recover your investment costs.
 <br />
<b> When will the company be listed on the MSM index?</b>
 <br />
 <br />
 The IPO will be opened for public for 30 days. So we are expecting to close it on June 13. And then you have 15 days of allotment and listing. So the company will be listed by the end of June. As per the agreement with the government, we have to be listed no later than July 1.
 <br />
 <br />
<b>  After the IPO what will be the shareholding pattern?
</b>
 <br />
 Since the founder shareholders are offloading a part of their capital, the shareholders’ hold will decrease and become 37.75 for Veolia, 29.25 for National Power and Water and 35 per cent for the public.
 
 <br />
 Once we are listed, the management normally will remain unchanged however, the board of directors will increase from five to seven directors. So, 45 days from the IPO offer closes, we will call for an OGM to elect the board of directors. There will be five from the founding shareholders and two from the public. Any member of the public who holds Sharqiyah Desalination shares can stand for the elections. To stand for elections as a member of our board, you need to own a minimum 100 shares.
 
 <br />
 <br />
<b> Can you share details of the company’s operations?</b>
 <br />
 The capacity of this plant is 80,000m3/day of drinking water. Every year we have a performance test in which we have to show in 24 hours consecutively, we are able to produce to our full capacity. Once we show this certificate, our revenue is assured by the client for the forthcoming period. The fixed part of the revenue represents 80 per cent of the revenue. And then the remaining part is as per the output. So that means, even if the client does not take the output (which never happens usually), we are entitled to invoice as per the capacity. That is the agreement with our client which is the Public Authority for Electricity and Water (PAEW). We have a offtake contract for 20 years from the commercial operation date which was from October 2009. 
 <br />
 The desalination plant is operated and maintained by Bahwan Veolia Water Company owned by Bahwan Engineering Company and Veolia. This company invoices to Sharqiyah based on 60 per cent of the plant capacity and 40 per cent of the output.
 <br />
 It means even if the client is not taking any water during the contract period, SDC will invoice 80 per cent to PAEW and the O&M company will invoice 60 per cent.&#8194;
