						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The Sharqiyah Desalination Plant’s initial public offering which is open till June 13 is good value for money for the public, says Philipe Da Costa, Chief Financial Administrative Officer, Veolia Water, Oman Branch, in this interview with Visvas Paul D Karra. Excerpts

</font></b><p align="center" style="margin: 3px 10px"> 
					&nbsp;<p align="center" style="margin: 3px 10px"> 
