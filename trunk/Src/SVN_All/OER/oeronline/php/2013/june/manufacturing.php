					<b><font face="Verdana" size="4" color="#C00000">From Steel to Luxury Cars</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The integrated steelworks have enabled Hyundai Motor Group to complete the development of the world’s first environmentally friendly resources recycling business structure. An OER report</font></b>
						<br />
						
						<center><img src="images/manufacturing_1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">
 
From molten metal to finished cars, and from Korea to the world, Hyundai Steel is heralding a new era of change and innovation in the world’s steel industry. It is doing this by developing an eco-friendly production system that is firmly grounded on the “3Rs” – reduce, reuse, and recycle.
<br />
<br />
 Hyundai is perhaps among the only two auto manufacturers in the world that have their own steel manufacturing plants (the Indian auto major Tata Motors is the other one). With an annual production capacity of 20 million tonnes, Hyundai Steel has emerged as one of the world’s top ten steel makers. By September this year, the company is expected to complete its current expansion phase to increase its annual production capacity to 24 million tonnes to move up further in the rankings of the world’s leading steel industry players.
 
<br />
<br />
 
 Unlike other steel plants, Hyundai Steel is committed towards environment protection in a very serious manner. Take this. Hyundai Steel is the first steel maker in the world to develop a fully-enclosed raw materials storage and blending system that keeps raw materials from escaping into the environment in inclement weather conditions such as floods and typhoons. In addition to demonstrating the company’s commitment to technology based environmental management, the system has been benchmarked around the world for its obvious environmental protection strengths. The company has also been credited with being the first steel company in the world to develop a resources recycling business system that stretches all the way from iron ore to finished vehicles, to scrap steel and all the way around again. This system has allowed the company, which is the largest steel resources recycler in Korea, to enhance its global competitiveness through the continuous operation of its blast furnaces.
<br />
<br />
 Crude and hot-rolled steel that has been produced at Hyundai Steel then progresses to cold-rolled steel production at Hyundai Hysco and vehicle manufacturing at the plants. The final step comes when the cars are turned into scrap for recycling at the Automotive Recycling Centre. The scrap steel is then converted into reinforcing bar and H section for use in construction and building projects being undertaken by other group companies including Hyundai Engineering and Construction and Hyundai Amco. Their waste construction materials are in turn recycled as scrap steel, completing the cycle once again.
 
<br />
<br />
 A company spokesperson said: “Using iron ore and coking coal, Hyundai Steel produces hot-rolled coils and steel plates for use in automobiles, ships, home appliances, and machine tools. The company is known for producing high-quality, specialised steel products to meet the needs of each industrial area and enhance its competitiveness.” 
<br />
<br />
Speaking about the automotive steel sheet, the spokesperson said: “Steel sheets that are used for car frames and exteriors must be high in strength and resistant to fatigue, since they are directly linked to a car’s life span and safety. In addition, they must be lightweight and easily malleable, so that they can be turned into a wide range of shapes. In order to satisfy these various requirements, Hyundai Steel produces specialised hot-rolled steel sheets, improving their development and speed of production by conducting research projects.”
<br />
<br />
The increasing frequency of earthquakes around the world has sparked interest in the development of earthquake-resistant steel. In order to accommodate this growing market, Hyundai Steel is accelerating the development of a broad range of such products. The company has already succeeded in developing hot-rolled H section and ultra-strong reinforcing bar with greater earthquake resistance, and are enhancing their reliability by introducing a quality guarantee system. 
<br />
<br />
Hyundai Steel’s R&D centre has also succeeded in developing a high-quality steel plate for ships and other global demands. “We undertook intensive research to develop high-quality, easily malleable products that could meet the need for high-intensity, low-temperature weldability and high-heat input welding. The world’s top ten shipping classification organisations have certified our steel plate for use in ship construction,” the company spokesperson added.&#8194;
