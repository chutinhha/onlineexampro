					<b><font face="Verdana" size="4" color="#C00000">Renaissance Services figures in Forbes Middle East’s Top 500 listing</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Omani companies, representing the services sector, made it to the Forbes Middle East’s list of The Top 500 Companies in the Arab World and Top 100 Making an Impact</font></b>
						
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">Renaissances Services has been ranked number three in the Services sector listing of the Forbes Middle East’s The Top 500 Companies in the Arab World, underlining the unprecedented opportunities that are available for value-driven innovative companies.
<br />
<br />
Shell Oman Marketing Co, Oman Oil Marketing Co and Al Maha Petroleum Products Marketing are also featured in this list of top 10 services companies.
The Top 500 Companies in the Arab World ranking has been formulated after an in-depth study by Forbes Middle East into companies listed on stock exchanges across the region. Companies from 10 countries made it onto the final list, achieving combined total revenues of $342.6bn, net profits of $61.8bn, and total assets of $2.215trn.
 
<br />
<br />
The listing has been drawn from various sectors like banking, financial services, transport, telecom, services, retail, real estate and construction, petrochemicals, investments, insurance, industrials, health care, hotel & tourism, food industries and energy. 
<br />
<br />
Forbes Middle East has reported that the well diversified services sector includes 51 companies across a range of businesses. The combined revenues reached $8.4bn last year and net profit amounted to $816mn. 
<br />
<br />
In terms of number of entries, Saudi Arabia came out on top with 123 companies featuring on the list. Runner-up where volume of entries is concerned was the UAE with 87 companies including familiar names such as First Gulf Bank and Etisalat (which ranked third overall). Jordan followed in third place in terms of number of entries on the ranking, followed by Oman, Kuwait, Qatar, Egypt, Bahrain, Lebanon, and Iraq, respectively.
</font>
