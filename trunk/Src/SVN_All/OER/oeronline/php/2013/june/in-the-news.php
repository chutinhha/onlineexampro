<b><font face="Verdana" size="4" color="#C00000">Omantel VP HR bags GCC HR Professional Award</font></b>
<br />
<br />
<center><img src="images/inthenews_1.jpg"></center>
<br />
<p align="justify" style="margin: 3px 10px">

<font size="1" face="Verdana">Omantel vice president of human resources unit, Dr Ghalib Saif Al Hosni, was awarded ‘HR Professional of the Year’ at the fifth annual GCC HR Excellence Awards ceremony held in Dubai. The second day of the regional summit centered on the growing role played by HR professionals. The event was organised to recognise excellence in HR and management practices within GCC public and private organisations and acknowledged individuals and organisations.
<br />
<br />
“We are very proud of this award on the GCC level especially because the award recognises individuals and organisations that make tangible achievements in developing human resource,” says Al Hosni. “Despite the tough competition from the local and regional companies, our excellence secured us this prestigious award. Omantel’s HR unit attaches great importance and care to developing its human resources as the management has a firm belief that human resources is the most valuable asset for the company and the main driver behind Omantel success story”.
<br />
<br />
Dr Amer Al Rawas, Omantel’s CEO bagged HR focus award in the fourth annual GCC HR Excellence awards 2013.
