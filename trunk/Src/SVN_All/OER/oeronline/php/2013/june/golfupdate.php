					<b><font face="Verdana" size="4" color="#C00000">Feeding frenzy</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The Greg Norman-designed Almouj Golf witnessed a hectic season with five competitions in a month’s time</font></b>
						
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">
Almouj Golf recently had a packed schedule with a slew of competitions sponsored byits partners. Kicking off the hectic season was The Chedi Muscat Monthly Medal, where players competed for a coveted spot in the last tournament of the season, the ‘Tournament of Champions’, in which golfers vied for the ‘Champion of Champions’ title. With a gross 96, net 76, Yvonne Welling won the ladies division, while 2013 Men’s Club Champion Tenzin Tsarong came in first in the gross title, followed by October Gross winner Kevin D’Amours in second place. The Net title went to John Prescott, whose gross 84 off a handicap of 17 yielded a 67 net total, the second best winning net score of the season. 

<br />
<br />
 The Almouj Golf Magnolia Medal was the second tournament, organised in parallel with the first Major of the year. In order to connect with the Masters Golf Tournament 2013 at the Augusta National Golf Club in Georgia, US, each golfer drew a player from the Masters field and combined their net score to their professional counterparts. The winner of the individual category was Allan Broadhead, whose score of net 62, including 4 net eagles, set a tough target for other golfers. Danny Renihan came in second place with a net 63. Taking the counterparts’ scores into account, Catherine Shackleton took top honours with her net 66 combined with Mark Leishman’s 66 to bring it up to 132. Dale Morris came in second with his net 66 combined with Dustin Johnson’s 67 for a total of 133. 
 
<br />
<br />
 The Almouj Golf Swiss Air Par 3 League Series 1 Final followed, with ten players making the cut from the previous six weeks of qualifying. The golfers competed for a pair of Business Class flights to Dubai, courtesy of event sponsors Swiss Air. Nasaruddin Mohammad took top honours with a fine performance of nine straight par 3s for a net total of 24, while Johan Keyter and Robert Daniel shared a net score of 26, only to have Daniel come in second after a countback over the last six holes. 
 
<br />
<br />
 Almouj Golf held its first annual Black Tee Challenge, for which all 7310 yards of the course was used. Many Almouj Golf members as well as several of the Omani National Team and other low handicap members from other clubs in Muscat competed to score the best 36-hole Gross total in order to win the title of Black Tee Champion 2013. The title eventually went to Azaan Al Rumhy, with a 163 combined score; Aaron O’Hara took runner up spot with a two-day total of 165 and Tenzin Tsarong’s 168 placed him in third. 
 
<br />
<br />
The next was Mercedes-Benz Member Guest Event, the second in a new series of events designed to increase participation in both competitive and social golf. Coming in first were Almouj Golfmembers Captain William Welsh, Chris Hanks and Peter Bell with a gross 64 and a net total of 60.9 and a Mercedes-Benz wrist watch prize for each player. In runner up position came Michael Lenarduzzi and guests Leon Patchett and Liam McAuliffe whose score of gross 68, net 63.4 rewarded them with a Mercedes-Benz hamper each.&#8194;
