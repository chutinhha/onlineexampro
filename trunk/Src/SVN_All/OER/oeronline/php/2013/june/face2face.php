<font face="Verdana" size="1">
Shatha Al Maskiry, Managing Director, Protiviti Member Firm, Middle East speaks about the consultancy’s operations, job creation and Oman’s economic prospects in an interview with OER
<br />
<br />
<b>Can you give us a brief background of Protiviti – its global operations and solutions that it offers for clients?
</b><br />
Protiviti is a global consulting firm that provides solutions around business performance improvement, risk and compliance, IT consulting, corporate governance, restructuring and litigation, transaction services, internal audit and financial advisory. We also offer other services that complement our key solutions such as human capital consulting, ISO advisory services and SME consulting.
<br />
Through our network of more than 70 offices in over 20 countries, we have served more than 35 per cent of the Fortune 1000 and global 500 companies. We also work with smaller, growing companies, including those looking to go public, as well as with government agencies.


<br />
<br />
<b>How long has Protiviti had a presence in Oman, and how has its experience been in the Sultanate’s market?
</b><br />
Protiviti has been actively operating in Oman for the last five years and has experienced tremendous success in the market year-after-year serving most of the top 50 clients in sectors such as banking, oil & gas, utilities, telecommunications, insurance and numerous government entities. We also served and partnered with regulatory bodies such as the Central Bank of Oman, Capital Market Authority and Information Technology Authority for major events around corporate governance, leadership in IT, and risk management.
<br />
<br />
<b>What are the verticals/industries that Protiviti specialises in and what differentiates your solutions from other consultancies?
</b><br />
Protiviti in the Middle East region is highly specialised in industries such as telecommunications, utilities, banking, oil & gas, and government; however, we have served a number of other industries at a large scale such as real estate, hospitality, manufacturing and transportation and shipping.
Protiviti prides itself on being able to offer an experienced and well qualified consulting base. This is leveraged for the benefit of both clients and our younger workforce being able to offer both tried and tested solutions as well as innovative solutions.

<br />
<br />
<b>Protiviti has been a strong believer in knowledge sharing. What is the idea behind this and how can companies access the wealth of information and insights generated by the company? </b>
<br />
Protiviti believes in sharing its wealth of knowledge and it is offered as a complimentary access on our global website. There are substantial resources such as whitepapers, resource guides, regulatory updates and newsletters, presentations, case studies, and webinar recordings. The idea is to provide insights on current business challenges that would be of interest to the board, CEOs, CFOs, CIOs, general counsels and CAEs. This is our way to share ideas and ways for companies to best address their business issues in a timely manner. We are proactive in every aspect; not just via our website but we also deliver workshops to interested companies as and when industry-specific issues arise.
<br />
<br />
<b>What are your thoughts on Oman’s economy – its prospects, opportunities and challenges?
</b><br />
Our labour force is rapidly growing so we need to focus on devising sound diversification and privatisation strategies to create more jobs. There are pockets of great potential as well as challenges but what matters is when and how fast we manoeuvre to embrace them tactfully; and this starts by focusing on our most important resource; human capital. Having said that, we need to really emphasise on the basic foundation of good and continued education because knowledge is power and it is the only sustainable asset that will empower this nation in the long term because as we speak, our natural resources are getting depleted and we really have no time to waste.
<br />
We need to heavily invest in enhancing the education system and harnessing technology to improve all levels of learning as this will be a key pillar of growth in our economy. We are in a critical time where we cannot afford to fail the younger generation; they have to be equipped to face and tackle the challenges ahead. We need to focus on enriching basic education, higher education as well as vocational training and a lot of attention has to be placed on our youth ‘bulge’ so that we can churn out more innovative thinkers who have the ability to communicate in multifaceted ways and ultimately continue to build this country. In my opinion, this is the most critical investment that needs to be made with the objective to ensure these investments are circulated and retained within our country for a sustainable and promising future for the nation.

<br />
<br />

<br />
<b>Job creation for Omanis has been identified as a national priority by the government in Oman. What measures can be taken by the corporate sector to further this goal without compromising on their efficiency and competitiveness?</b>
<br />
This can easily be achieved when both the government and private sector join forces to make this happen. It cannot happen overnight but for example, with the SME development programme and fostering entrepreneurship; this will create jobs as and when small businesses grow and expand. Also, when business environments are enabled with flexibility and policies that do not become an obstacle to job creation; it will immediately enhance the demand for labour.
<br />
The private sector will always be happy to create jobs but will continue to be focused on quality rather than the number of jobs. We need to see that the productive jobs are surfacing and the less productive jobs are dissolving because what matters is that the right jobs are created to transform our community at large and demonstrate stability and growth. It is essential to identify the types of jobs required for the specific development of Oman and remove any particular obstacles that prevent the creation of such jobs.
<br />
The corporate sector in Oman continues to heavily invest in training Omanis through various means and they also develop the skills of their employees through on-the-job training. With the rising Omanisation rates that are imposed on the private sector; this is a blessing as it is the only chance for Omanis to really upgrade their skills and become competitive and efficient because the market is already aggressively attracting the talented Omanis to ensure they are making sound human capital investment; not just to develop the individual or organisation; but also the nation at large. I trust that the corporate sector will continue to ensure lifelong vocational training available to allow Omanis to eventually stay at the cutting edge of the global skilled workforce. It is no longer a choice; it is a valued need today.

<br />
<br />
<b>As the Eurozone continues to struggle and the US witnesses a slow recovery, do you think that the Middle East and emerging markets are going to be the drivers of future growth?</b>
<br />
There is a trend whereby the emerging markets are sinking but that also varies on certain countries and regions as well its political stability, young demographics, currency stability and government economic incentives. Nevertheless, our Middle East market certainly has unyielding grounds for continued growth where oil and gas is concerned and can be a great opportunity for value oriented investors to gain entry at the right place, time and price.&#8194;
