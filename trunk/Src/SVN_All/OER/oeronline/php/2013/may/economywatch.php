<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">
Oil production may be continuing to increase in Oman, but the Sultanate’s focus remains firmly fixed on preparing for a post-oil future. In March, it was announced that despite concerns over the longevity of its oil production capacity, Oman’s average daily output rose to 918,000 barrels per day (bpd), outdoing the 2011 average of 884,900 bpd, according to statistics from the Ministry of Oil and Gas. Yet while this offers a cushion, it does not provide a long-term answer to sustaining economic growth. Instead, recent indications suggest that the Sultanate is turning its attention towards attracting greater levels of foreign direct investment (FDI) as a way of boosting growth and driving diversification.
<br />
<br />
Of course, Oman has long welcomed foreign businesses, offering 70 per cent corporate ownership to outside investors. However, spurring new efforts is the fact that FDI in Oman fell by 31 per cent to $788mn in 2011 (the last year for which figures are available) – the lowest level since 2004, according to data from the United Nations Conference on Trade and Development. While this is likely to be the result of a more cautious spending environment following the global economic downturn – rather than a reflection on the Sultanate itself – the Omani government is leaving nothing to chance.
<br />
<br />
The Sultanate has adopted a three-pronged strategy for the promotion of foreign participation within the Omani economy. First, infrastructure and support services are receiving a funding boost, further developing the local framework for international businesses to set up shop. Second, changes are now being made to the regulatory environment, and the Ministry of Commerce and Industry is revising the Foreign Capital Investment Law with a view to projecting the Sultanate as a more attractive investment destination. And third, greater efforts are being made to promote emerging and ‘niche’ sectors – such as the ICT sector and cyber-security – to develop regional competitive advantages.
<br />
<br />
Last October, it was announced that the Sultanate plans to invest $450mn to extend its free trade zones across the country to attract more foreign investment. These extensions include expanding land, building warehouses, offices, roads, staff accommodation and storage facilities, according to the Free Trade Zones Authority. Indeed, this has already had an impact; Sohar’s free trade zone has since drawn interest from some 14 foreign companies investing in metals, minerals and logistics.
<br />
<br />
A further aim of encouraging foreign investment is job creation. With its fast-growing population of some two million citizens, there is a need to create tens of thousands of jobs every year; jobs that the hydrocarbons industry cannot generate. Currently, Omanis make up 14 per cent of the private sector labour force, according to Central Bank statistics. It is hoped, therefore, that this figure will increase as more foreign firms employ Omanis.
<br />
<br />
But the government is going further. In February, it was announced that The Ministry of Commerce and Industry is revising the Foreign Capital Investment Law, with a view to projecting the Sultanate as a more attractive investment destination. In addition, while specific details were not available at the time of writing, it is expected that included among the reforms will be revisions to employment regulations for foreign firms, bridging gaps that were previously exploited to recruit expatriates over Omanis. The revisions are also widely expected to address procedures for granting commercial registrations, and will prepare plans for existing and future job-generating projects.
<br />
<br />
Of course, the fall in foreign investment registered in 2011 was not only experienced by Oman. Given the ongoing ‘lag’ effects of the global economic crisis, FDI inflows into the wider GCC decreased in 2011 by 35 per cent – from approximately $39bn in 2010 to $25bn in 2011, according to United Nations Conference on Trade and Development statistics. However, going forward the region as a whole is expecting an uptick in foreign investment, as figures return to healthier pre-financial crisis levels. Furthermore, the outcome of this increased competition for FDI among GCC states is driving efforts to develop unique selling points, or areas of specialisation that are hoped will draw in foreign firms in emerging and growth markets.
<br />
<br />
One of the key target areas for FDI in Oman is information and communication technology (ICT). The ‘Sas Programme’ – officially launched in mid-2011 but now picking up increased momentum – seeks to develop an internationally-competitive ICT industry in Oman, by attracting more foreign and local investment in the ICT sector, encourage and support innovation by contributing to the introduction of new technologies, and encourage entrepreneurship and the start up of new businesses. The programme also seeks to facilitate ICT ‘networks’ which allow foreign and local businesses to connect and help each other develop and grow. It is hoped that this will create ‘knowledge transfer’ clusters of ICT businesses, and establish Oman as a technology hub in the region – the positive spin offs of which will also benefit the broader economy.
<br />
<br />
One specific area identified for growth is the cyber-security industry, and the authorities are supporting the establishment of local companies, especially SMEs in the sector, and encouraging foreign investment in this field. According to Oman’s National Computer Emergency Readiness Team (OCERT), seven local cyber-security companies have been set up in the last two years, with more set to start operations in the future.&#8194;
