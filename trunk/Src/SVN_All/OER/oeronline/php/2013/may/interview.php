					<b><font face="Verdana" size="4" color="#C00000">Success redefined</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Jack Canfield, motivational speaker and author, speaks about his books, acting and the Transformational Leadership Council in a freewheeling chat with Mayank Singh

</font></b><p align="center" style="margin: 3px 10px"> 
					&nbsp;<p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/interview_1.jpg" width="300" height="162">
					<p align="justify" class="style13">
<b>					Chicken Soup for the Soul is your path breaking work. How did the idea of the book come about?
</b>										<br />Chicken Soup for the Soul happened because I started out as a high school teacher and found that when I used stories my students paid more attention and as a result I got much better achievement out of them. So I started collecting stories to motivate them and I did it for years and then I started training teachers and found that if I used stories it was very helpful in that situation too. Eventually, I was giving a talk and someone came up to me and asked whether the story that you narrated about the girl scout, the boy with no hands and legs etc is that in a book and I replied – ‘No.’ This happened almost everyday for almost a month. One day I woke up and decided that I needed to put these stories in a book. I was flying from Boston to Los Angeles, where I was living at that time and I wrote down the names of all the stories that used to narrate. In all I had around 79 stories and I thought that it was enough for a book. I made a commitment that I would write a story every three and a half days, so I would have two stories every week and a hundred stories by the end of the year and that’s what I did. One day I was having breakfast with my friend Mark Victor Hansen and he asked me as to what I was doing and I told him that I was writing a book of stories and he said that he wanted to write it with me. I told him this is like telling a famous author who has written three quarters of a novel to help him finish it, and so why should I do that? He said, one, some of the stories that you tell, you stole from me and second, I am a better marketing and sales person, and we would make a great team. So we teamed up and surely we were a great team and that’s how the series got started. As books are done in folios, our publisher said there are three blank pages at the end of the book asking us whether we would like to fill them. On one of the pages we wrote a note to our readers stating – ‘if you have a story send it to us and maybe we will do a sequel.’ And here we are 225 books later.

					
					<br />
					<br />
					<b>Did you ever expect the book to achieve such cult status?</b>
					<br />
					No, no, not at all. I thought we would have a best-selling book and we had studies on how to do that. When our publisher asked us whether we had a goal? we said ‘yes we would like to sell one and a half million books in a year and a half’ and he said ‘that is not going to happen’ and he said ‘That is not going to happen,’ and we said ‘You watch.’ He retorted – ‘You guys are crazy and we said – ‘No we are entrepreneurs and we know how to do business.’ We sold 1.3 million books in one and a half years, so we missed our goal but our publisher stopped laughing at us. 
					
					<br />
					<br />
					<b>The Success Principles is dissimilar to Chicken Soup, why did you feel the need to write something that is different?</b>
					<br />
					People kept on asking me – ‘How did you become so successful’ and I have always been a student of success. I have been teaching some of these principles to teachers and so on. I also got kind of tired of Chicken Soup, you know when you are reading an inspirational story and not getting moved then there is something wrong. So I decided to take a break. I was thinking as to what I could do that was interesting and new and that would make a difference and I thought why not take all these principles and put them into a book. One day I sat in front of my computer and noted down all the principles that had enabled me to become successful. I came up with over 115 and my publisher said – ‘No way.’ So I started combining some, eliminating others and we got it down to 64. Then I thought, what if this is just unique to me and so I embarked upon interviewing 75 of the most successful people in North America and I showed them my principles and asked them whether they used the same principles. If they said yes, I would ask them as to how they used them and their stories are featured all across the book to illustrate these principles. I wanted people to realise that these principles are not just Jack Canfield’s principles but universal.
					
					<br />
					<br />
					<b>Can someone be coached to be successful or is it an innate quality?</b>
					<br />
					It is innate in all of us, what happens is that in some of us it gets programmed out of us, but it is possible to learn again. People are naturally motivated to achieve, grow and to expand their capacity. Unfortunately, on the way due to pressure from the school, parenting, peers etc we start to contract and pull back. As a result people start to constrict and they are not as motivated as they should be because of the fear of failure or being judged. We all have the natural capacity to succeed, we have talents that we are born with. Some people unquestioningly have more talents. We have seen Renaissance people like Michelangelo who could paint, be a scientist or a Leonardo Da Vinci, but everyone has talents and we all have the ability to express those. These principles are totally teachable to anybody in any situation. We have seen people from third world countries who have become entrepreneurs; kids who were gang members become straight citizens and do very well in life; prisoners have been rehabilitated.
					<br />
					
					<br />
					<b>You have acted in a number of movies, how did movies happen and do you see yourself as a movie star?</b>
					<br />
					It is interesting, When I was in high school, my cousin was an actor, he had studied acting and I drove him once from West Virginia to Virginia to Summer Stock theatre where he was acting and I saw so much energy around the place that I thought to myself, ‘It would be fun to be an actor.’ So I wrote on my goal list, I want to be in a movie. Initially, I thought it would be real acting job even if it was a two minute role where a person walks into a bar and talks to you. At that time Rhonda Byrne was making a movie called The Secret and she asked me to participate in it. According to most feedback, I am the top five people in the movie and I enjoy that and then everyone started making other movies as they saw the success of The Secret, but no one has matched its success, but I got invited to be a part of a number of movies like The Opus, The Truth, Mega Secret and in all I have acted in 11 movies and the one that I just did was known as The Keeper of the Keys and in this I actually got to act.

					<br />
					<br />
<b>					You have initiated something called Transformational Leadership Council (TLC) with thought leaders, trainers and motivational speakers from across the world. Why should people who compete with each other join the same platform?</b>
<br />
Many of us do not think that we are competing with each other. We come with the conviction that more happens from collaboration than from competition. We found that a lot of us were on the top of our fields and there was no professional association, where we could meet. I felt the need to speak to my contemporaries and I must confess that I was a bit nervous initially, but I invited people and 32 of them came to my house. We now have 140 members from across countries and we are growing by 15-20 members every year in the core group. Then we started something called Associates of Transformational Leadership (ATL). So if there is someone who feels that there are 20 people from Oman who should be a part of TLC, then they could start an ATL.&#8194;
