<font face="Verdana" style="font-size: 8pt">
The new CEO and new owner of Chronoswiss, Oliver Ebstin recently visited Oman to promote the brand and share the future growth plans with Fahad Al-Araimi, CEO of Al-Raid Jewelry, the authorised agent for Chronoswiss Watches in the Sultanate. 
<br />
<br />
A part of the Sayarti Group, Sayarti Autocare has over the years grown into one of the most popular and successful brands within the automotive care industry in the Sultanate. The company’s full range of personalised quality auto services and premium products has taken the market by storm, raising its profile as a leading name in the Auto Care business in just a few years of operation. 
<br />
<br />
The basic concept behind Sayarti is quite simple – offer a one-stop-shop for all customers’ car care needs whilst offering top quality service, the very best products, affordable prices and convenient locations. While the concept seems simple it is the execution that has earned the brand the popularity it enjoys today.
<br />
<br />
The staff at Sayarti Autocare ensure that the customers always comes first, so each and every activity, either selling the products or providing services, is done in a professional manner to ensure that the customer gets the best possible quality. Whether customers are looking for a simple car polish - or something more specialised such as Llumar window tinting, getting their vehicle’s wheels refurbished or maybe even full car detailing – Sayarti Autocare’s team of motoring experts are always on hand to give owners the very best advice when it comes to caring for all types of cars and SUVs. Additionally, one of the key aspects of the business is to ensure that the prices for its services are kept affordable. As such, customers can expect a premium service and the benefits offered by top-notch car care products without breaking the bank.
<br />
<br />
As mentioned earlier, in addition to being an all round auto services provider, Sayarti Autocare uses top brands like Glasurit Automotive Refinish, Cooper Tires, Mickey Thompson Tyres, Sonax Car Care Products, HBC Smart Repair Products, Llumar window tint and PPF, and Kenwood in-car entertainment products and more, to ensure that customers enjoy the best possible results. These products represent the crème-de-la-crème of the automobile servicing and care industry and have helped Sayarti Autocare become the ‘go to’ brand for customers in Oman when it comes to automobile care.
<br />
<br />
There is more to Sayarti Autocare than just car servicing. It is a brand that offers services that combine automotive care with the health and appearance of a vehicle. These services include vehicle servicing, tyre balancing/replacement/repair, wheel alignment, minor electrical jobs, battery replacements, AC gas charging, brake check and repairs, engine flushing, fuel injector cleaning, window tinting, polishing and paint protection, installation of car audio systems and smart repair for all brands of vehicles in addition to several other customised services for motor heads in the Sultanate. 
<br />
<br />
One of the most popular services Sayarti Autocare provides is Llumar window tinting. This product not only gives drivers – and their passengers – protection from the sun, but also from dangerous glass shards should the windscreen become damaged in the event of an accident. On the other end of the spectrum, Llumar’s paint protection films provide another great benefit to customers by protecting the front end of the vehicle. Head lamps, the front bumper and fog lamps are prone to get damaged from stones, sand and other debris one will encounter on a day-to-day drive on the road. Protection offered by this film customers can minimise costly repairs and replacements. These films are well known for their quality and colour stability and the patented scratch resistant coating makes them durable and virtually maintenance free.
<br />
<br />
Revitalising a vehicle’s showroom look is an art and one that has been perfected at Sayarti Autocare. As a result, car detailing is done with the utmost care at the autocare outlet. At the brand’s exclusive service centres, as per a customer’s request, the professionally trained experts at Sayarti can carry out interior and exterior detailing and paint protection of the highest quality. Using renowned Sonax car care products, Sayarti’s skilled technicians revitalise a vehicle’s looks and can ensure it lasts for the longest possible time. To make the most of the shine provided by the detailing process, a vehicle needs to be free of dents and scratches. To address this, Sayarti offers customers another specilised service to carry out such repairs, by way of SMART Repair.
<br />
<br />
The concept of Small Medium Area Repair Technology is called SMART Repair and the technology that supports it was originally created to offer the motor industry a fast, cost-effective yet environmentally friendly solution to assist in the cosmetic repair of used vehicles. Historically, damaged vehicles were sent to traditional bodyshops for all cosmetic repairs – no matter how small. This resulted in vehicle being unusable for an extended amount of time and also devalues the vehicle at the time of resale.

<br />
<br />
Carheal SMART Repairs by HBC Systems offers complete solutions and thanks to Sayarti, these services are available to all consumers in Oman. Sayarti offers the whole range of SMART Repair Solutions which include Fabric and Velour repair, plastic, leather and vinyl repair, Plastic welding systems, Spot repair and car paint repair systems, Windscreen repair, Wheel Rim repair systems, Paint scanning and paint reader systems, Paintless Dent Removal and Odour Removal. In fact, the experts at Sayarti even use sophisticated colour analysis software and professional photo-spectrometer equipment to provide the exact ‘recipe’ to deliver perfect colour matches every time. This way the paint fix is indistinguishable from the rest of the vehicle.
<br />
<br />
In a country such as Oman, where excellent road infrastructure allows high speeds, it is essential that automobile tyres are always in perfect shape. A visit to the Autocare’s Tyre Fitment Centre can guarantee quick and efficient tyre servicing and a one-stop-shop for all their tyre care needs. Sayarti also showcases brand-new, quality tyres from two world-renowned brands – Cooper Tires and Mickey Thompson.

<br />
<br />
For those customers who are specific about lighting in their automobiles, Sayarti’s automotive lighting division offers VisionX off-road LED lighting solutions that are fitted by trained technicians. VisionX is an interesting addition to Sayarti’s existing product line up as it offers one of the best engineered lighting systems in the market and nurtures a strong reputation for building high quality lights, customised for automobiles for off-road, industrial usage, mining, rallying, etc.
<br />
<br />
Most automobile owners are also passionate about their car audio systems and Sayarti has become a fan-favourite for the excellent products and services they provide in this area as well. Whether it is a simple replacement or complicated upgrade or a pure custom job, Autocare’s audio experts can design and fabricate packages to suit every desire and tastes of discerning customers. Better still, Sayarti is one of the more prominent dealers for Kenwood car infotainment systems, thereby ensuring that customers can truly enjoy their music on the go.&#8194;
