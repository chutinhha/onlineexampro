The combination in June 2012 of Oman International Bank and HSBC Oman to form HSBC Bank Oman (HBON) has brought together two organisations with a rich history in the country. The result is a stronger and larger financial institution in the Sultanate, with a considerably expanded domestic network and a truly international presence. 
<br />
<br />
At the date of the merger, the combined entity became the third largest bank by total assets, and had the second largest branch network in the Sultanate. For both entities, the merger increased scale and international connectivity and for OIB customers, provides immediate access to HSBC’s global product suite.
<br />
<br />
The merger represented HSBC’s first merger in the Middle East and North Africa region for many years and provided the HSBC Group with a rare opportunity to increase scale and commitment to Oman. In the months following the merger, the overriding priority of the management team has been the operational, strategic and cultural integration of the two entities to ensure that customer expectations are met. 

<br />
<br />
Under challenging conditions, significant progress was achieved, thanks to the commitment, patience and support of loyal customers and the hard work and dedication of all of the employees.
<br />
<br />
<b>Milestones
</b><br />
<br />
Systems integration was completed in November, providing all HBON branches with access to an international standard banking and management system, supporting effective and consistent procedures across every branch 
HBON joined the OmanNet National Switch following the merger, providing the customers with immediate access to well over 1,000 ATMs across the Sultanate
The ATM network has also been updated extensively, with 107 units replaced and upgraded 
The management team has conducted a full review of the business, aimed at improving organisational efficiency and the sustainability of operating model 
As many as 21 branches have been refurbished, with 60 scheduled for completion in 2013

<br />
<br />
<b>Future outlook
</b><br />
<br />
HBON’s immediate strategy is dedicated to the completion of the integration and transition processes. This will drive the investment in the branches, people and systems, in order to support customers’ needs and deliver on strategic priorities. In 2013, the bank will shift its focus to the growth plan for the business, developing existing relationships, connecting its customers to more opportunities and delivering an increasingly consistent and superior customer experience.
<br />
<br />
“HSBC Bank Oman recognises its responsibility to invest in the long term prosperity of the community in the Sultanate of Oman. To us, a long term, intergenerational view is a prerequisite of true business sustainability. Our Corporate Sustainability strategy focuses on education, environment and the community.”
