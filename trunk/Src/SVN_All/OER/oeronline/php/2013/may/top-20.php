<br />
Oman Refreshment Company performed well against tough market conditions such as the highly price-sensitive consumers, changing consumption habits and a highly competitive juice, water and snacks product segment

<br />
<br />
Oman Refreshment Company (ORC) is a leader in the Omani beverages market, holding the franchise rights to fill and distribute PepsiCo range of soft drinks and Aquafina water as well as trading in non-carbonated beverages and Frito Lays range of snacks throughout the Sultanate. Since its inception the company has played a key role in the soft drinks and beverages business segment in Oman.

<br />
<br />

ORC has purpose-fit production and bottling plant as well as central warehousing facilities in Al Ghubra, Muscat and 10 depots situated al Al Wafi, Ibri, Nizwa, Saham, Salalah, Bukha, Buraimi, Musannah, Liwa and Mudhaibi that cater to the customers spread across the Sultanate.

<br />
<br />
The company invests significantly and regularly on the upkeep of current production lines, its distribution capabilities and marketing equipment with a focus on improving operational efficiencies as well as increased customer reach. Through its subsidiary, Al Rawdha Integrated Trade & Investment Enterprises, ORC is constantly exploring suitable new business growth opportunities.

<br />
<br />
<b>Milestones
</b><br />
<br />
ORC has aptly recognised the expanded warehousing needs and embarked on strategic expansion of warehousing capacity at various business locations to improve operational and marketing efficiencies.
ORC has successfully implemented green initiatives starting with the establishment of policies and mechanism to efficiently manage the waste generated during its production/ bottling process.
The company has carried out numerous learning and development activities for its staff during 2012 and continues to invest in its human resources.

<br />
<br />
<b>Looking Ahead
</b><br />
<br />
ORC shall continue to invest suitably in business system and replacing aging assets. The company is optimistic about the future prospects with the product expansion and diversification plans and renewed impetus on achieving higher production efficiencies. However, the volatile prices of key input raw materials, packing materials, unstable job market and rising wage bill and stagnant consumer prices may impact profitability of the company in the near future.

<br />
<br />
“The rising demand for products with high quality and diversity combined with growing health conscious consumers offers a promising future for food categories in which ORC operates”
