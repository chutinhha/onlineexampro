<br />
<br />
“Our in-country value ethos ensures we deliver an international world-class standard, with genuine local content that benefits the economies and communities in which we serve. We do this through local employment, local procurement, local ownership and our Corporate Social Responsibility programmes. Being serious about local content does not require any compromise on price, quality or compliance. On the contrary, it requires embedding competitive capability locally to support our international brand.”
