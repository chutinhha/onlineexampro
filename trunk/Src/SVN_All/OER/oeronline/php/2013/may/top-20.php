Renaissance is creating value through the development of two world-class, internationally competitive businesses – Topaz – a global Offshore Support Vessel (OSV) shipping company and Renaissance Contract Services Group (CSG) – an international Integrated Facilities Management (IFM) company. This is being achieved by being a model public company and following the values of good governance, transparency, business ethics, financially sound and secure (strong balance sheet) and best practices (HR, Financial, Legal; Systems and processes). The company is also a model oil & gas professional services provider which is: safe (no harm to people); efficient (cost-effective, compliant, quality services); green (no harm to the environment) and local (serious about in-country value (ICV)).
