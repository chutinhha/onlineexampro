Following 30 years of successful growth, the Sultanate’s flagship bank is poised to further consolidate its leading position by generating synergies between various business units led by competent staff
<br />
<br />
As the leading Omani bank, Bank Muscat accounts for a 38 per cent market share with assets of $19.8bn. The bank enjoys investment grade credit ratings and is rated ‘A1’ by Moody’s and ‘A-’ by Standard & Poor’s. The bank has an extensive network of branches in Oman, direct and indirect presence in all six GCC states as well as an office in Singapore.
Bank Muscat enjoys the largest shareholding by the Omani government while members of the senior management team are in place for over 10 years. The bank is well positioned to leverage on the large network of branches and other delivery channels to target the growth potential and cross-sell opportunities. Bank Muscat’s presence has strengthened in all parts of Oman through the widest network of 136 branches, 553 ATM/CDMs and 5,300 PoS terminals.

<br />
<br />
<b>Milestones
</b><br />
<br />
Five new branches were opened and evening banking service was extended to eight branches across the Sultanate
<br />
<br />
Savings deposits portfolio rose to RO1.34bn in 2012, a 21 per cent growth over 2011
The first-of-its-kind Al Wathbah Business Zone launched for SME customers who do not have their own office space to be used free of cost

<br />
<br />
Investment banking business had a record year in terms of fees and successfully closed transactions raising debt and equity of over $1.45bn during 2012.
<br />
<br />
The Asset Management business saw its assets under management (AUM) surpassing the $1bn mark
The bank received many foreign, regional and local awards including 373rd rank among the top 1,000 banks in the world by The Banker; the Hawkamah Corporate Governance Award; the coveted Sultan Qaboos Award for Excellence in e-Government for its mBanking mobile services etc

<br />
<br />
<b>Outlook</b>
<br />
<br />
The new ‘We Can Do More’ vision reflects the strategy of the bank for the coming period. The opportunities identified by the bank include major infrastructure development projects and government focus on economic diversification and tourism development. Every year over 50,000 Omanis are entering the workforce and the government has announced higher minimum wages for them which will lead to new banking opportunities.
<br />
<br />
The year 2013 will be a period of increasing competition for the banking sector with the launch of Islamic banking operations, competitive channels and new products to cater to the various new and growing sectors.
<br />
<br />
“Following 30 years of successful growth, the Sultanate’s flagship bank is poised to further consolidate its leading position. The new ‘We Can Do More’ vision reflects the strategy for the coming period. The bank is well positioned to generate synergies between various business units led by competent staff, who have recorded exemplary career achievements and leadership skills and come on board the management team.”
