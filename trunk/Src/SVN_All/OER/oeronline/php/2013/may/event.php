						<span class="style27"><strong>Oman Forum 2013 on May 6</strong></span><p align="left" class="style13" style="line-height: 17px">
						<b><font size="2"><p align="justify" style="margin: 3px 10px" dir="ltr">

						Leading experts from the Government, economy and society are going to debate on the most important agendas before the nation, in Oman Forum 2013. The event is taking place on Monday, 6 May at Oman Auditorium, Al Bustan Palace Ritz-Carlton. HE Darwish bin Isma’eel bin Ali al Balushi, Minister Responsible for Financial Affairs, is the chief guest for Oman Forum 2013.
						<br />
						<br />
						<p align="center"><img src="images/events_1.jpg"></p>
						</font><font size="1" face="Verdana">
						<br />
						<br /><p align="justify" style="margin: 3px 10px" dir="ltr">
						</b>Apart from senior bureaucrats from the relevant ministries, over 400 strong audience representing the senior-and-middle level leadership of private sector and public sector companies; civil society leaders; and higher education students are going to attend the forum. The noted experts, in the form of two panel discussions, would discuss the challenges related to the pressing issues of job creation and SMEs development and propose solutions to be implemented by the relevant stakeholders.
						<br />
						<br />
						Talking about the event, Mundhir Al Barwani, External Affairs Director in Petroleum Development Oman (PDO) and a panellist in Oman Forum, said: “I’m delighted to be a panelist in Oman Forum 2013. It will be an excellent public platform to discuss about various dimensions related to the job creation – the most important agenda before the nation. The objective of creation of jobs, training and business opportunities in large numbers is intrinsically integrated in PDO’s business model of exploration and development of oil and gas. Along with our contractors, we have created over 4,000 jobs since His Majesty’s National Objectives Programme was launched in 2011. This year we are committed to continue with our drive with a number of skilled Omanis employed by our contractors.”
						
						<br />
						<br />
						The other panellists include HE Mohsin al Balushi, Advisor, Ministry of Commerce & Industry; HE Tawfiq Al Lawati, Member-HR Committee, Majlis A’Shura; Ahmed Al Wahaibi, CEO, Oman Oil Company; Salim Naseer Al- Hadhrami, Director General - Planning and Development, Ministry of Manpower; Eng. Saleh Al Shanfari, Chairman, A’Saffa Foods; Hussain Salman al Lawati, MD, Oman Cables Industry; Said Al Hamdani, AGM & Head-HR, Ahli Bank; Salah Al Mawali, DG-SME, Ministry of Commerce & Industry; Abdullah Al Jufaili, GM, Sharakah; Hussain Ghuloom, CEO, Al Waqia Shoes; Ilham Al Hamid, AGM-SME, BankMuscat; and Hassan M. Juma, MD, Mohammed Juma Sultan Co. Noted TV anchor Yusuf al Hooti would moderate the Forum.
						<br />
						<br />
						The event has been supported by the Ministry of Manpower, Ministry of Higher Education, Ministry of Civil Services, Sultan Qaboos University, OPAL, Oman Society of Contractors and The Research Council (TRC). Oman Oil Company, Ahli Bank and Galfar are the strategic partners whereas Petroleum Development Oman (PDO) and Oman Cables Industry are the associate partners; and Omantel, Nawras, Jindal Shadeed Iron & Steel and Al Jazeera Steel Products Company are the support partners for the prestigious forum. Times of Oman and Al Shabiba are the media partners for the event.
