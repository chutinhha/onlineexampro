					<b><font face="Verdana" size="4" color="#C00000">An Irish golf day</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Audi Oman partnered Oman Irish Society to organise the 2013 edition of the Irish Society Golf Classic, at Ghala Valley Golf</font></b><p align="justify" style="margin: 3px 10px"> 
					<img border="0" src="images/golf_update_1.jpg" width="525" height="266"><p align="justify" class="style13">
Wattayah Motors, the official dealer for Audi in Oman, partnered the Oman Irish Society to organise their annual golfing event – the Irish Society Golf Classic 2013 at Ghala Valley Golf Club.
Commenting on the event, Ahmed Shariefi, general manager of Audi Oman, said, “Over the last two decades, Audi has supported golfing tournaments across the globe. Technology, design and sportiness are values that characterise every Audi vehicle and are also reflected in the sport of golf. Our association with the Irish Society Golf Classic is yet another example of our ongoing commitment to the sport.”

<br />
<br />
 The Oman Irish Society is a social organisation, which offers friendship and a fun environment to connect Irish people, forming a community in the Sultanate. At the event the course was a flurry of green and clover-leaf printed golfing gear, as all members of the Oman Irish Society came together to try and score a hole-in-one.
<br />
<br />
 In Oman, the passion for golf has been on the rise. To accompany this upsurge in interest, a large number of golf courses have been developed, in accordance with international standards. Audi Oman also recently hosted the second edition of the Audi quattro Cup series in the Sultanate. The Audi quattro Cup is one of the largest and most popular amateur golf tournaments in the world. More than 75,000 golfers compete worldwide, and it has become one of the most sought-after competition opportunities for Audi owners and golf club members throughout the world, and also in the Middle East.
<br />
<br />
 Audi’s involvement in sporting events aims to support people in the pursuit of personal excellence and precision.&#8194;


