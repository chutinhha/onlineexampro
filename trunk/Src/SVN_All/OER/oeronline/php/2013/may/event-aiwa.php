						<br />
In a red carpet awards evening on 16 June, AIWA will felicitate the top performing listed companies in Oman during 2012. Overall 15 top ranking companies will receive the coveted trophy. A networker’s dream, the event will be attended by the who’s who of Muscat corporate high life. The premium event will attract more than 250 top business leaders, CEOs and senior government officials from a cross-section of industries, ministries and government bodies. It will witness the congregation of the key decision-makers who are driving the country’s business and economy. A high-voltage entertainment show by renowned international troupes will make the attendees cherish the evening for a long time to come.						
