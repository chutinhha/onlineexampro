<p class="style15" style="line-height: 14px">
Your comments on the performance of Standard Chartered in the last one year
The bank is in good shape. This is our tenth consecutive year of income, profit and dividend growth, despite external challenges. Our balance sheet remains well diversified with high liquidity and strong capital position. The diversity of our businesses and resilient business models have enabled us to weather the economic ups and downs, and sustain our trajectory.
Globally, Standard Chartered income growth rose to 8 per cent to $19.07bn, wholesale banking income up 9 per cent to $11.78bn and consumer banking income up 6 per cent to $7.20bn. In Oman, the bank’s income rose by 28 per cent while operating profit surged by 68 per cent.

<br />
<br />
<b>In which areas did your bank witness more growth?
</b><br />
<br />
We have made great progress on the Consumer banking front through our SME, wealth management and retail lending businesses. We have recorded an impressive growth with customer loans and advances increasing by 63 per cent and customer deposits by 56 per cent.
Most banks thrive on infrastructure development financing. 
<br />
<br /> 
Does Standard Chartered follow a different working model?
<br />
<br />
We have a clear client-centric strategy, providing trade finance, cash management, securities services, foreign exchange and risk management, capital raising and corporate finance solutions. Standard Chartered has been in Oman for 45 years hence we are well positioned to meet our clients’ needs.
<br />
<br />
<b>Please comment about your expansion plans in Oman
</b><br />
<br />
We have expanded our operations to three branches with the recent opening of our Qurum Branch. The bank has invested in technology and improving turnaround time hence we had launched personal loan in 60 seconds to enhance our customers’ banking experience.
<br />
<br />
<b>What is the roadmap for the future?
</b><br />
<br />
We will continue to serve the needs of our clients with world-class products and services, and leverage our international network as a competitive advantage. We’re in the right market and we have a clear strategy. In consumer banking we continue to focus on the high value segment which includes SMEs, and priority banking. We are investing for long term growth focusing on technology (digitisation). We continue to focus on developing Omani nationals through our Al Massar Programme which is designed to provide Omani nationals with tools and the skills necessary to improve their performance and enhance development. Developing future leaders remains one of the highest priorities for the bank.

<br />
<br />
<b>You have the backing of your parent bank, but mostly the local balance sheet is important for your operations. Your comments
</b><br />
<br />
Standard Chartered Bank in Oman is a branch of the Standard Chartered group, hence we have the strength of the group’s balance sheet to leverage on. Our local balance sheet is also very strong and meets all the local regulatory requirements. We have the capacity to make sizable loans to meet the needs of our corporate clients.&#8194;

