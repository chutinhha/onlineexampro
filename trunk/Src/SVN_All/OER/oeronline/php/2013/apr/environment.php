						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Growing 
biofuel crops 
</font></b></p>
						<p align="justify" style="margin: 3px 10px; line-height:17px"><b><font face="Verdana" size="2">
                		<br>
						An SQU-funded research project has evaluated the ability of Jatropha plant to grow under different saline and water stress conditions in Oman and their potential in mitigating desertification   <br /></font></b><p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
There is a dire need for sensitive management of soil and water salinity in Oman if its Agriculture is to be kept alive and the ever increasing desertification process is to be mitigated at all. Growing salt tolerant crops should be one of the major areas for salinity research in the Sultanate. Experience shows that different crops will behave differently if they are grown under different saline and water stress conditions. Pot and field trials will clarify their ability to grow and produce.
<br />
<br />
Jatropha Curcas is an excellent biofuel crop which has many advantages over existing crops. It grows almost anywhere, even on gravelly, sandy and saline soils. It can thrive on the poorest stony soil and grow in the crevices of rocks. The hardy Jatropha is resistant to drought and pests, and produces seeds containing up to 40 per cent oil. When the seeds are crushed, the resulting Jatropha oil can be processed to produce a high-quality biodiesel that can be used in a standard diesel car, while the residue can also be processed into biomass to power electricity plants.
<br />
<br />
It is imperative that Jatropha be tested in Oman with regard to its suitability for commercial production. In 2008, there were communications between the College of Agricultural & Marine Sciences, Sultan Qaboos University and some Indian Institutions to initiate a collaborative research project.  In the first stage, four kilograms of certified seeds were received and a formal SQU funded research project was initiated. The purpose of this study was to evaluate the ability of Jatropha plant to grow under different saline and water stress conditions and with/without fertiliser applications.
<br />
<br />
The study concluded that the Jatropha plant can grow well under saline and drought stress conditions, but the plant should be transplanted or initiated in the winter season or when the average temperature is around 30°C. The best plants from this study were transplanted in plots where they grew well and produed some seeds. The findings of this preliminary study were used as the bases for a further study with different parameters.  
<br />
<br />
Preliminary results show that plant growth was good in all treatments even in saline and drought stress treatments. .Plants treated without any addition of fertilisers were weak and showed symptoms of deficiency. Inorganic fertiliser gave the best result. But, plants grown with organic fertiliser performed reasonably well. The growth of plant treated with organic fertiliser was much better when the plant was irrigated with saline water.
<br />
<br />
Organic fertiliser can enhance leaching process leaving the root zone in better condition compared to inorganic fertiliser which is a source of salts. With regard to organic and inorganic fertiliser, a mixture of organic and inorganic fertiliser works best. Perhaps the addition of organic matter improves soil structure (bigger pores), allowing better leaching as well as improved water and nutrient holding capacity; whereas the inorganic fertiliser only provides nutrients (and increases salinity somewhat because of its chemical nature).&#8194;
