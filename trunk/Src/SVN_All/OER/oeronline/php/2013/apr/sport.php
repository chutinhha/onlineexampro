<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">
It may not have been high speed racing on the final day of 2013 Extreme Sailing World Series but it was as tense as it gets, and in classic Extreme Sailing Series style, it came down to the final race for the winner to be decided. Defending champion Leigh McMillan and his team on The Wave, Muscat sailed a picture perfect day coming back from a 15 point deficit to Red Bull Sailing Team to claim overall victory, in the closest possible finish on their home turf.
<br />
<br />
Just two points separated Red Bull Sailing Team and The Wave, Muscat going into the final double-pointer and the maths was simple – whoever performed better in that race, would win the Act. The invitational home team, Team Duqm Oman -with the winner of the inaugural Series back in 2007 Robert Greenhalgh at the helm- won the race, but The Wave Muscat did enough to secure victory after a tense battle.
Red Bull Sailing Team skipper Roman Hagara exchanged handshakes and slaps on the back with crew after the final race of the four-day event, knowing that Red Bull’s title challenge this season is more than wishful thinking.

<br />
<br />
Hagara could, however, be forgiven for wanting more from Act 1 out of the eight series after going into the final day of racing with a 15-point lead following superb sailing in the three previous days of competition.
Defending champions The Wave, Muscat shot out of the blocks to clinch three wins in the first four races on Friday and overtake the Austrians, grabbing victory at the very final race of the day, finishing just ahead of their rivals with. For double Olympic champions Hagara, and tactician Hans Peter Steinacher, the four-day season premiere was an overall success despite the bittersweet finale, as they seek to clinch the overall title in this campaign after coming extremely close last year.  

<br />
<br />
The Wave, Muscat ended up with 232 points, six ahead of Red Bull. America’s Cup winners Alinghi grabbed third spot with yet another solid day’s of sailing on 218 and their first podium position since 2011.
The Extreme Sailing Series is an adrenaline-pumping event with the vessels reaching top speeds in narrow stadium courses. More than a million people around the world have already witnessed the spectacular racing first hand. Innovations for 2013 include longer coastal races and a special format for Saturday evenings. The races are stretched over four days with one day water racing and three in stadium mode in front of spectators.&#8194;

