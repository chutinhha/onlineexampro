The company continues to adopt a conservative approach to credit approvals as a result of a perception of heightened credit risk since Q4 2008. Within this policy, the approach has been to target customers perceived to meet the company’s credit risk appetite. As a result, net finance assets increased by 17.8 per cent to RO126.69mn as compared to RO107.52mn in 2011. Overall, the company achieved a net profit of RO4.11mn for the year ended December 31, 2012 as compared to RO3.55mn in 2011, an increase of 16 per cent. The company has raised equity capital of RO7.5mn by issuing 75,000,000 shares to existing shareholders at par value of 100 baizas per share during February 2012. The rights issue was fully subscribed and accordingly the paid-up capital of the company has increased to RO25.05mn which enables the company to satisfy its regulatory capital requirements for the foreseeable future as per current regulations.

