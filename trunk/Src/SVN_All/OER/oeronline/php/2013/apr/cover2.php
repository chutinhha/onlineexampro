<br />
<br />
A major milestone achieved by Al Omaniya, this year, is that the company’s share market capitalisation crossed the $75mn mark, thus catapulting it into the large cap segment on the Muscat Securities Market. This is a first for the NBFC industry. The company’s loan book recorded a growth of 32.11 per cent over the last year and stood at over RO205mn at the year end. The net income grew from RO10.8mn in 2011 to RO12.5mn in 2012. 
<br />
<br />
The gross revenue during the year 2012 grew by 18.07 per cent and stood at RO17.05mn as compared to RO14.44mn in the previous year. The net interest income grew from RO10.3mn in 2011 to RO11.9mn in 2012. Though the cost of funds has come down moderately from the previous year, the spreads are lower due to comparatively low yield in the new product segments and corporates, and due to intense and unhealthy competition. In spite of the above, the company was able to register yet another record profit of RO5.65mn for the year 2012 producing earnings per share of 32 baisas on the increased share capital. There is also a 20 per cent appreciation in its share value which means the market has recognised the company as a niche NBFC.  The company has sound human resource policies and its Omanisation percentage stood at 80.8 per cent.
<br />
<br />
The competition from the well capitalised finance companies as well as the existing commercial banks will be intense and lead to severe reduction in margin and asset quality. This coupled with two new Islamic banks starting operations and directly competing in the retail segment to make their presence felt will drive margin further down and could see erosion in profitability and asset quality.&#8194;
