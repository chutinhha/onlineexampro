					<font color="#333333" size="2"><b>Standing tall in their own right<br></b></font>
					<br />
				
				<p class="style15" style="line-height: 14px">	<font size="1" face="Verdana">
				The non-banking financial companies (NBFCs), licensed by the Central Bank of Oman (CBO), grew by 23 per cent in 2012 in terms of asset size. During this period, the combined profit after tax of this industry grew by 17 per cent</font> 
