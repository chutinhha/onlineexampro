                    <hr color="#C0C0C0" size="1">
<p class="style15" style="line-height: 14px">
The leasing and finance companies also known as the non-banking financial industry has six licensed players, with a total asset size of over RO700mn and total market capitalisation of RO231mn. Few local commercial banks have also set-up retail financing and SME division on a significant scale adding to the competition in the industry, apart from the recently launched Islamic finance window of almost all the local commercial banks. In addition to these regulated financial institutions, there is substantial availability of credit from the unregulated sector. The suppliers of equipment and automobiles also act as distribution channels for finance as a measure of sales aid and backward integration.		
<br />
<br />
The financial markets in the Sultanate continued to remain stable. The money market, government securities market and foreign exchange market fall within the jurisdiction of the Central Bank of Oman (CBO), while the capital market, corporate debt market and the insurance are regulated by the Capital Market Authority (CMA). As financial markets play a very critical role in financing the growing needs of the various sectors of the economy, the CBO continued to provide support to the markets within its jurisdiction. The objective is to develop deep, liquid and vibrant financial markets in the Sultanate.
<br />
<br />
Capital market in Oman remained subdued during 2012, despite recovery of the economy. Although medium term fundamentals of the economy are robust, stock market activities mostly reflected unimpressive growth of profits of the private sector and fragile global recovery. But the financial sector was the most active sector comprising almost 67 per cent of the total securities traded and 47.5 per cent of total turnover during January-September 2012 followed by the industrial sector with a share of around 19 per cent in the total securities traded and 27.9 per cent of total turnover during the same period.			
<br />
<br /> 
<b>Macro indicators
</b><br />
<br />
The macro-economic indicators continued to be favourable during the year 2012. Boosted by surging demand, oil prices continued to remain firm, enabling government to once again post substantial fiscal and balance of payments surpluses. This improved confidence in the economy, encouraging the government to announce a large number of projects, which include the Southern Express highway (second packet), the Batinah coastal road, the major expansion of Muscat and Salalah airports, the Duqm Port, a number of internal road projects in Dhofar, the Sharqiya and elsewhere in the country.
<br />
<br />
The private sector too, has risen to the occasion stepping up investment in the ‘tourism’, services, manufacturing and construction sectors. The increased economic activity, and the policies implemented by the Ministry of Manpower resulted in the creation of large number of jobs for Omani nationals and contributed to the demand for automobiles.

<br />
<br />
The banking sector continued its positive growth trend in 2012, consistent with the sustained growth of the economy. Banks remained sound, resilient and profitable due to appropriate regulatory and supervisory policies adopted by the CBO. Liquidity in this sector improved sharply, with deposits of the commercial banks exceeding the lending opportunities. Most of the banks have set up a separate SME division, and are facing similar challenges as NBFCs. 
<br />
<br />
The advent of Islamic Banking is expected to diversify banking services and expand financial inclusions. Two new banks have established their presence, and with most of the local commercial banks having launched Islamic banking, the availability of funds is likely to increase substantially.
<br />
<p class="style15" style="line-height: 14px">
<center><img border="0" src="images/cover_chart_13.jpg" width="500" height="629"></center>
					<font size="1" face="Verdana">
<br /><p class="style15" style="line-height: 14px">

<b>Demand is huge</b>
<br />
<br />
The demand for this sector as was seen during the last three years when the corporate segment has also seen growth which is expected to continue as the eighth five-year economic development plan (2011-2016) approved at the beginning of 2011, includes a government capital investment of RO12bn over the next five years, while the total government expenditure will amount to RO42bn for the five-year period. The enhancement in the minimum capital requirement and the increased combined net worth of this sector; create a capacity of about RO1bn in the industry. In addition to this, during the year 2013, two Islamic banks are going to be active in their operations. This excess capacity could be a constraint as the sector is unlikely to grow by 100 per cent to fill up the potential.
<br />
<br />
The retail segment also witnessed a growth in the year 2012 because of the measures, such us creation of employment opportunities, higher minimum salary, welfare benefits for the registered unemployed. The growth in this segment is expected to continue as the eighth five year plan aims to generate 200,000 to 275,000 new employment opportunities to the national workforce.
<br />
<br />
<b>Performance</b>
<br />
<br />
The year 2012 again saw Al Omaniya Financial Services retain its position as the largest NBFC in terms of asset size, profit, EPS, net worth and market capitalisation thanks to its strong fundamental ratios, in earnings, yields, assets and lowest NPAs. Muscat Finance, the country’s long-standing non-banking financial company, has reported a year of improvements in profits and uninterrupted dividends since inception with 21 per cent growth in assets and eight per cent growth in net profits.
<br />
<br />
Oman Orix Leasing Company has recorded highest profits and business volumes in its history with new business volumes increase by 24 per cent to RO69.33mn on purchase cost basis. United Finance Company recorded a good performance during the year 2012 thanks to the strengthening of its relations with vehicle and equipment dealers and cashing in on the market to grow its loan book, albeit with emphasis on asset quality. National Finance Co continued to enjoys a robust sourcing, underwriting, collection and operational model commensurate with the size and risks of the underlying asset class.
<br />
<br />
Taageer Finance Company achieved a 22 per cent growth in profit after tax of 
RO3.9mn in 2012 as against RO3.2mn in 2011, which is higher than the industry 
average of 17 per cent.<br />
<center>
<img border="0" src="images/cover_chart_14.jpg" width="500" height="194">
</center><br />
<p class="style15" style="line-height: 14px">
<b>Source of SME funding
</b><br />
<br />
NBFCs are major source of funding for the SMEs and with the strong encouragement of government initiatives, the sector can play a significant role in the economic development of the country. The initiation of various infrastructure projects will trigger a lot of economic activities and employment generation through the trickledown effect.
<br />
<br />
Increase in oil prices, good growth in government budget, softening of interest rates and other positive factors may provide the necessary impetus for growth opportunities.  There has been higher stress on the sector to sustain the earnings ratios with the increase in nonperforming assets, shortage of talent and increase in manning costs. Another concern is the interest rate, which is the lowest at present. This may go up any time in 2013 and all companies are expected to take cautious steps to overcome this adverse situation. Companies with the sustainable business model with emphasis on higher efficiency and innovation will succeed in the long run.
<br />
<br />
<b>Opportunities and challenges<br />
</b><br />
The projected growth in the economy through infrastructure development has resulted in the demand for equipment financing while the diversification of economy to non-oil sectors (transportation, trading and tourism sector) will continue to create the demand for financing. Factors such as the government’s effort at developing a national work force, increasing employment of young Omani nationals are expected to create opportunities for retail financing.
<br />
<br />
These opportunities have made the leasing sector one of the preferred sectors which is reflected in the balance sheet build-up and bottom line profits of all the companies. However, challenges are expected to arise within the industry, thanks to the excessive supply through professionally managed NBFCs and local commercial banks apart from suppliers of equipments and automobiles. The high indebtedness of local population and resource deprived SME sector and the aggressive in-house financing by major automobile dealers are expected to have an impact on the NBFCs.
<br />
<br />
<b>Outlook
</b>
<br />
<br />
The country’s budget envisages substantial expansion in infrastructure investments and also calls for the creation of a large number of jobs in the public and private sectors. In addition, the eighth five year planned expenditure has increased substantially. These developments are positive for our SME and retail businesses.&#8194;
