<p class="style15" style="line-height: 14px"><br />As part of our new initiative to bring on board the leasing sector performance, OER-GBCM has commenced its annual ranking of the Best Leasing Companies in Oman starting this year. For this annual ranking process, we have considered the growth and sustainability parameters of the leasing companies in Oman (similar to our successful Best Banks ranking model with few changes relevant to the sector). The leasing companies ranking model was done among the local players which include Al Omaniya Financial Services, National Finance, Muscat Finance, United Finance, Taageer Finance and Oman Orix Leasing. We have taken financial data for the period from 2009 to 2012 to rank the leasing companies on the set parameters for Growth and Sustainability. 
<br />
<br />
The data were sourced from the published financial statements (audited) of the leasing companies. Ranking based on the growth is done by calculating the compounded annual growth rate (CAGR) of gross leasing assets (ex. unearned finance income), net interest income, operating profit, networth and net profit over 2009 to 2012. In our ranking model, the operating profit is calculated by subtracting total operating income with the total expenses (excluding depreciation costs). Ranking based on the sustainability factor was done by calculating the four-year average of three key factors which includes asset quality, productivity and efficiency. Asset quality was ranked based on non performing assets to the gross leasing assets (NPA/GL) ratio and the overall leasing assets provisions to non performing assets (provision coverage ratio). The productivity is ranked based on the operating profit of the company to its total number of branches and number of employees. The efficiency factor considers net interest margins (net Interest income/average interest bearing assets), cost to income ratio, return on average assets (RoAA) and return on average equity (RoAE). For the calculation of cost to income ratio, we have excluded the depreciation expenses.

