					<p class="style15" style="line-height: 14px">
<b>How was Ahlibank’s performance in year 2012?</b>					
<br />
<br />
If you look at the global and the local challenges in Oman and see what we have done in the past three to five years, including the last year, we are very pleased with our achievements.  We have continued to do well in various areas of our operations and have outperformed other banks in different areas. If you look at the banks business, our commercial banking operations has done well in terms of growth. I have always insisted on the fact what is important is not just growth, but also the quality of our growth. Our NPAs are the lowest in the industry and we will try and maintain it at that level. Our return on assets is still the highest in the country although the cost of funds has become higher. Our returns per employee is the best. So if you look at most of the parameters whether they are related to profitability, efficiency or quality of assets, we are pleased to see where we are. However, we are aware of the fact that it is a challenging market and with two new banks entering the market competition is bound to grow. You will soon see our first quarter results and we hope that we can continue on the same track.
<br />
<br />
<b>You have always insisted that it is the quality of growth that matters more. Having attained a certain size, wouldn’t it be fair for you to aim higher? </b>
<br />
<br />
When you are building an institution you need to build it not just for today, but also for the future. Every institution has its corporate culture and credit philosophy and you do not change it just because you are growing. The culture has to be a part of the institution’s DNA going ahead. Yes we are a conservative bank and we are not going to water down our credit norms, just because we want to grow. Probably, we would be open to shrinking the size of our balance sheet to have better quality of assets, than just loading the balance sheet with toxic assets. So while our balance sheet will continue to grow, we will be focussed on the quality of assets that we book. 
<br />
<br />
<b>&#8207;Does this conservative approach compromise your growth prospects?</b>
<br />
<br />
It compromises on our growth prospects in areas that we do not want to be in, which is clear and we are very happy about it. Not all growth is good and if other banks are happy to take that kind of a growth, let them do it, but for us if there are areas that we do not want to go, then we will stay away from them. However, if there is a business that we are interested in then we do everything to win that business and that’s the reason for our growth. 
&#8207;Ahlibank’s proposed acquisition of Taageer Finance has been in the news. 
<br />
<br />
<b>What is the purpose of this takeover?</b>
<br />
<br />
Going forward, banks have to play a major part in the economic growth of the country, and what is being talked about in a big way now is SME development. If you see the customer profile of finance companies and the customer profile of banks, finance companies do more of SME kind of transactions and that is an area where we want to grow in future.  So Taageer would be a platform for us to expand our SME business. The leasing business also fits in very well with Islamic products, so if you mix Islamic products and the growth prospects in the SME business, it gives us a very strong platform to launch both at the same time and that is an area of growth for us going forward. 
<br />
<br />
<b>Will Taageer Finance be merged into Ahlibank or will be a separate entity?
</b>
<br />
<br />
As per CBO rules, it has to be a part of the bank. In terms of how we are going to run the merged entity, we will decide on that after doing the due diligence, as the process will tell us exactly what we want to do with the transaction. We have to understand their customer profile, quality of assets, skill sets of their staff, policies in terms of operations. Once we have the data then we can come up with a better strategy. 
<br />
<br />

&#8207;The proposed acquisition shows that you are looking at inorganic growth apart from organic growth. 
<center><img border="0" src="images/cover_chart_6.jpg" width="500" height="291"></center><br />					<p class="style15" style="line-height: 14px">

<br />
<b>Will the bank be pursuing more such opportunities in future?</b>
<br />
<br />
Inorganic growth is very much a part of our strategy and we have been saying this earlier. If we find opportunities that fit in with our shareholder requirements then we will definitely look at those possibilities. 

<br />
<br />
<b>Have you increased your branch network and ATM presence in the last year?
</b><br />
<br />
When you increase your branch network it is meant to serve two purposes: business growth and servicing the clients better. In terms of growth in the retail business, it is not going to be as high as in the previous years on the asset side but on the liability side we see a lot of growth and our focus is on gaining low cost deposits and retail deposits. We continue to expand our branch distribution, to grow our business and to serve our clients. In February 2013, we opened five Islamic banking branches in a day and this is another area where we see a growth in our retail business. 
<br />
<br />
<b>With two new banks coming into the market, a lot of people think that Oman has too many banks. What are your thoughts on the issue? 
</b><br />
<br />
My view is that we are seeing too many banks in Oman, if you compare Oman with the neighbouring countries which have larger economies and the number of banks that they have and compare them to our economy and population, we see that we have too many banks and when you add the branches of foreign banks it adds to the pressure. Going forward I feel that there is going to be some consolidation.
<br />
<br />
<b>&#8207;The government has identified Omanisation as a national priority. Is Ahlibank contributing towards this endeavour?
</b><br />
<br />
During the last three years, there has been a 25 per cent growth in recruitment on an annual basis and most of these have been Omanis -- 95 per cent of them are graduates. They are usually from the College of Banking and Finance or other Universities.  We are a young bank and we want to recruit young talent. We help young recruits to gain skills and have a clear career path for everyone in the bank. You will see Omanis occupying very critical positions in the bank and this is growing. In Ahlibank, various departments like finance, corporate, HR, corporate communications, credit risk, treasury, Islamic banking, operations, marketing and product development are headed by Omanis. They are assisted and guided by our expatriate colleagues. The skills that they are gaining ensures that they will be very good business leaders in future. 
<br />
<br />
<b>&#8207;Looking ahead, what are your thoughts on 2013?
</b><br />
<br />
The year 2013 is going to be a challenging year for every bank and for all business houses. But I also see a lot of opportunities as the government has ambitious plans to expand the economy and going forward, we are very optimistic about our numbers and prospects.  
