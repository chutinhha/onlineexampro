					<p class="style15" style="line-height: 14px">
<b>How has the Bank’s performance been in financial year 2012?</b>					
<br />
<br />
The key business lines of the bank recorded healthy performance during 2012. The bank achieved a net profit of RO139.2mn for the year ended on December 31, 2012 compared to RO 117.5mn reported for the year 2011, an increase of 18.5 per cent. Net interest income increased by 8.6 per cent to RO230.4mn in 2012 from RO212.1mn reported during the year 2011. Increase in net interest income was attributable to increase in asset growth during the year 2012. Non-interest income at RO93.2mn was higher by 13.5 per cent compared to the year 2011. The bank distributed 40 per cent dividend, 25 per cent in the form of cash and 15 per cent in the form of mandatory-convertible bonds.
<br />
<br />
In retail banking, the savings deposits portfolio rose to RO1,335mn in 2012, a 21 per cent growth over 2011, amounting to around 44 per cent marketshare. In step with the all-round growth of the economy, corporate banking had a stimulating year registering significant growth in both funded and non-funded business. The group re-structured its business lines and focused efforts towards active business segments resulting in robust asset growth. 
Investment banking business continued its market leadership with a stellar performance in 2012. The business had a record year in terms of fees and successfully closed transactions raising debt and equity of over $1.45bn during 2012. The bank has a strong pipeline with mandates of over $2.3bn under execution. 
<br />
<br />
Bank Muscat is the only bank in the Sultanate that offers the full suite of treasury products and services – whether hedging foreign exchange requirements or managing interest rate risks or eliminating commodity price risks from businesses to offering fixed income investment opportunities across the region and globe.
<br />
<br />
The asset management business crossed an important milestone during the year with assets under management (AUM) surpassing the $1bn mark.Meethaq, the pioneer of Islamic banking in Oman, recorded a momentous year in 2012, bracing itself for innovative Islamic banking widow operations with RO150mn capital.
<br />
<br />
<b>What are the measures that you have taken to strengthen your retail lending and corporate finance/project lending in the year gone by?</b>
<br />
<br />
The bank’s net loans and advances increased by 16.2 per cent to RO5,601mn in 2012 as against RO4,819mn as at December 31, 2011. Gross personal/housing loans at RO2,289mn increased by 17.1 per cent during 2012. Gross Corporate/other loans at RO3,523mn increased by 15.8 per cent. 
<br />
<br />
In project and structured finance, the bank maintained its leadership position by playing an integral part in several large-scale and key infrastructure projects. These include projects in key sectors such as oil and gas, petrochemicals, shipping, real estate, aviation and power and water.  During the year, the bank was associated with a major refinancing programme of a large IWPP project as a mandated lead arranger. In addition, the bank also participated in overseas syndicated transactions, which has increased its profile in the market.
<br />

<center><img border="0" src="images/cover_chart_7.jpg" width="500" height="273"><br /></center>
