<p class="style15" style="line-height: 14px">We have considered the growth and sustainability parameters for annual ranking of the commercial banks in Oman. The banks’ ranking model was done among the leading local banks which include Bank Muscat, National Bank of Oman, Oman Arab Bank, Bank Dhofar, HSBC Bank Oman (erstwhile Oman International Bank), Ahli Bank and Bank Sohar.
<br />
<br />
We have taken data for the period from 2009 to 2012 to rank the banks on the set parameters for growth and sustainability. For 2012, in the case of HSBC Bank Oman, we have incorporated the data of the merged entity. As a continuing process, the historical data in our ranking pertains to former Oman International Bank. During 2011, the newly established Bank Sohar became part of our ranking post completion of four years of operations along with the availability of data required. While in 2010, Ahli Bank became part of our ranking.
<br />
<br />
The data were sourced from the published financial statements (audited) of the banks, CBO reports and other published reports. Ranking based on the growth is done by calculating the compounded annual growth rate (CAGR) of gross loans, customer deposits, fee income, operating profit, networth and net profit over 2009 to 2012. In our ranking model, the operating profit is calculated by subtracting total operating income with the total expenses (excluding depreciation costs). Ranking based on the sustainability factor was done by calculating the four year average of three key factors which includes asset quality, productivity and efficiency. 
<br />
<br />
Asset Quality was ranked based on Non Performing Assets to the Gross Loans (NPA/GL) ratio and the overall loan provisions to non performing assets (Provision Coverage ratio). The productivity is ranked based on the operating profit of the bank to its total number of branches and number of employees. In conclusion, the efficiency factor considers net interest margins (net Interest income/average interest bearing assets), cost to income ratio, return on average assets (RoAA) and return on average equity (RoAE). For the calculation of cost to income ratio, we have excluded the depreciation expenses. 
