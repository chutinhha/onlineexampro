<b>Can you share details about your branch and ATM network. And is there any plan for upgrading your presence in the year ahead?</b>
<br />
<br />
The bank has the largest network of 136 branches, 409 ATMs and 144 cash deposit machines (CDMs) network across Oman. In 2012, five new branches were opened and evening banking service was extended to eight branches across the Sultanate.

<br />
<br />
Setting new benchmarks, Bank Muscat offers the complete range of banking products and services through the network of branches across the length and breadth of Oman. The sophisticated banking experience stems from the right mix of traditional and electronic channels. 
<br />
<br />
Innovations adopted by the bank are characteristic of the largest network of ATMs and CDMs found in all parts of the country. Bank Muscat acknowledges that smarter strategies are vital to stay ahead in business and provide a distinct customer service. The bank is focused on providing world class banking service at the doorstep of customers in all parts of Oman.

<br />
<br />
<b>Has the bank invested in its IT platform and what kind of efficiencies has this brought about to your operations? 
</b> 
<br />
<br />
A new state-of–the-art data centre has been put into operation at the head office and the production data centre was shifted from the old head office to the new facility. The disaster recovery and business continuity plans for Bank Muscat are of the highest standard and were tested successfully during the year. 
<br />
<br />
<b>What are the measures taken by the bank to improve its HR practices?
</b>
<br />
<br />
The bank embarked on a knowledge management initiative in 2012 to capture, disseminate and re-use its knowledge and competency based assets and has finalised the strategy to implement in 2013. This will result in sharing information, ideas and knowledge through the ‘communities of practice’ and learning from best practices and previous projects that will result in a culture of innovation and continuous improvement within the bank.

<br />
<br />
A process of identifying and grooming branch managers was institutionalised resulting in rounded development of identified staff prior to being placed in a branch manager’s role. This resulted in reduced learning curve for 42 new branch managers who were identified through this process.
<br />
<br />
The bank provided 5,976 learning opportunities through 552 courses to its employees in 2012. The programmes helped young Omanis to pursue studies at leading institutes and qualify to assume higher responsibilities in the bank.

<br />
<br />
<b>Can you tell us about your plans in FY2013?
</b><br />
<br />
The year 2013 will be a period of increasing competition for the banking sector with the launch of Islamic banking operations, competitive channels and new products to cater to the various new and growing sectors. 

<br />
<br />
On the back of buoyant economy, Bank Muscat expects its credit growth to be around 14-15 per cent this year. The bank enjoys an edge in hi-tech products and services, including electronic payment and web-based services, in tune with Oman moving towards a cashless society and meeting the banking requirements of a young and tech-savvy generation.

