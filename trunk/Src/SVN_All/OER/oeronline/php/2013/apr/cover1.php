<b>&#8207;How has the bank’s performance been in financial year 2012?
</b><br />
<br />
The year 2012 was an eventful year for HSBC in Oman as it witnessed the merger of HSBC Middle East Ltd.’s operations in the Sultanate with Oman International Bank to form a much larger, robust banking institution- HSBC Bank Oman. Even though the bank’s recently published financial results show a decrease in net profit, they reaffirm that our priority has been on the integration of the two entities. Integration expenses amounted to RO13.9mn which has contributed to a rise in total operating expenses to RO48.7mn for the year. The bank has recorded a 60.2 per cent increase in operating income and an 81.5 per cent increase in net interest income and we are now focused on achieving the growth opportunities present as a result of the combined entity. In the last two quarters of 2012, we were able to integrate two complex organisations. As a result, our board of directors are satisfied that we now have the organisational infrastructure and banking system in place to support our strategy for growth in 2013. When comparing 2012’s financial performance against that of 2011, we must remember that we are comparing operating costs of HSBC Bank Middle East Ltd.’s operations in Oman which had only five branches, against those of HSBC Bank Oman. We are now appropriately capitalised and have the proper infrastructure and banking systems in place to deliver sustainable returns on investments in years to come. 
<br />
<br />
<b>&#8207;The bank went through a merger with OIB. Has that process been completed?
</b><br />
<br />
 A critical part of any merger is the unification of data between two institutions which was completed in November 2012. This entailed brining more than 400,000 retail customers and 10,000 corporate clients under one banking platform. It also encompassed unifying internal banking systems of the entire branch network and streamlining working practices through staff training and development. In January 2013, rebranding of the entire HSBC Bank Oman network was completed to offer customers a consistent experience in terms of look, feel and service level in whichever branch they visit. 
<br />
<br />
Customers immediately felt the benefit of the systems integrations as it brought them services such as internet banking, PIN on IVR, SMS updates, dedicated contact centre support, and many other services that were previously unavailable to OIB customers including the ability to bank at any HSBC Bank Oman branch and not only their account holding branch. 
<br />
<br />
<b>What are the measures that you have taken to strengthen your retail lending and corporate finance/project lending in the year gone by?
</b><br />
<br />
HSBC Bank Oman continuously strives to adapt to market dynamics and meet customers’ changing lifestyles through improving existing propositions and introducing new products and services. We have made a number of lending policy changes to the benefit of our customers including, eliminating the need for OIB customers to provide a guarantor when applying for a personal loan. In addition, we have also introduced complimentary life insurance on personal loans. This has made HSBC Bank Oman’s retail banking loan proposition very competitive in the Omani market.
<br />
<br />
Our trade finance and international business continue to be at the forefront of our commercial banking proposition and our objective is to lead the way for Omani businesses, small and large, to trade with regional and global partners through international connectivity. HSBC Bank Oman also took a step to further strengthen its trade finance proposition through launching receivables finance which was successful and appealed to our clients as it opened up an attractive structured alternative to working capital needs. We have also invested in our on-the-ground payments and cash management proposition to strengthen our client’s coverage capabilities and commenced an aggressive rollout of tried and tested HSBC payments and cash management solutions in Oman.
<br />
<br />
<b>Can you share details about your branch and ATM network, and is there any plan for upgrading your presence in the year ahead?
</b><br />
<br />
HSBC Bank Oman operates more than 130 ATMs including more than 80 multifunction machines in high footfall locations across the Sultanate with a branch network of more than 80. Today, we are the second largest bank in Oman in terms of branch network and are constantly assessing our footprint to ensure we meet our customers’ needs. 
<br />
<br />
<b>Has the bank invested in its IT platform and what kind of efficiencies has this brought about to your operations?
</b><br />
<br />
In 2012, we made substantial investments in upgrading the bank’s infrastructure to build a robust platform on which customers could transact, HSBC Bank Oman’s business can grow and for our growing staff to work more efficiently and effectively. The investments in information technology included an upgrade of the entire communications network with connectivity between branches, offices, ATMs and our data centres. In addition, all former OIB branches received new computer equipment that meets HSBC’s global standards. We have also connected our entire ATM network to OmanNet National Switch Network, which has enabled customers to use any ATM across Oman, a privilege previously unavailable to former OIB customers. 
<br />
<br />
<b>&#8207;What are the measures taken by the bank to improve its HR practices?
</b><br />
<br />
We have made significant progress to integrate HR policies and procedures following the merger. We updated our employment proposition including the way individual performance is managed, how we pay all our staff as well as the level of benefits each employee can receive. We have also conducted a thorough review of salaries based on market analysis and job scope as we transitioned all HSBC Bank Oman employees to the HSBC Global Career Band structure. This resulted in the alignment of allowances, salaries and bonus levels, providing a solid foundation for future reward policy management and pay decisions based on the HSBC group reward strategy and group values.
<br />
<br />
Furthermore, we have also introduced several changes to employee benefits that are based on the philosophy of ensuring they are equal across the bank, irrespective of individual global career band. This included adjustments to staff loan benefits, alignment of club memberships and service awards, an extension of medical insurance coverage to all HSBC Bank Oman employees, adoption and paternity leave to former OIB employees, alignment of maternity leave with annual leave policy, as well as equal opportunity for business education and career development.
<br />
<br />
In terms of training and development, we have deployed a team of dedicated professionals to deliver training on HSBC’s retail banking systems. We have also conducted mandatory HSBC training on risk, compliance, money laundering, information security risk and fraud. Over 4,300 delegate days of training were given over 312 sessions in the second half of 2012 alone. &#8207;Learning and development in 2013 will be focused on HSBC’s strategy, products and systems. Programmes targeted for delivery include the introduction of the HSBC Performance Management system to all employees and a bespoke syllabus for customer service representatives and branch managers.&#8194; 
