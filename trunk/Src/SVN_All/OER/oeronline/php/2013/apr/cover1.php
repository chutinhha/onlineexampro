					<p class="style15" style="line-height: 14px">How was the banking sector’s performance in Oman during 2012?
The banking sector in Oman continued it’s positive growth trend in 2012 consistent with the overall GDP growth. The balance sheet of commercial banks further strengthened due to the robust growth in both deposits and credit. Total assets of commercial banks increased by 13.9 per cent to RO20.96bn in December 2012 compared to RO18.41bn a year ago. Total credit expanded by 14.4 per cent to RO 14.31bn at the end of December 2012 from RO12.51bn a year ago. Aggregate deposits held with commercial banks also registered an increase of 12.7 per cent on a year-on-year basis to RO14.2bn at the end of December 2012. The increase was broad based with saving, demand and time deposits rising by 17.2 per cent, 13.3 per cent and 7.4 per cent respectively. Provisional net profits of commercial banks stood higher at RO306mn in 2012 as against RO264mn in the previous year. 
<br />
<br  />
<b>What policy measures are being taken by the CBO to strengthen the banking sector in Oman?</b>
<br  />
<br />
A key element of the ongoing financial sector reforms in Oman has been the fine tuning of the prudential and supervisory framework through developing sound risk management systems, enhancing transparency and complying with international standards and best practices. The CBO had initiated a number of regulatory and supervisory measures recently to improve efficiency of the banking system. The minimum regulatory capital was recently raised from 10 per cent to 12 per cent of the risk-weighted assets. Risk-based supervision has been introduced and covers the entire banking system. The CBO is well within the time frame for the implementation of the Basel III capital adequacy framework. The Internal Capital Adequacy Assessment Process (ICAAP) has been operationalised by all banks in Oman with effect from December 31, 2012. A financial stability unit (FSU) has been set up within the CBO for macro-prudential supervision of the financial system and will be coming out with financial stability reports. On the operational front, the CBO reduced the interest rate ceiling on all new personal loans from eight per cent to seven per cent with effect from April 1, 2012. Also new micro prudential norms have been introduced for personal loans. The debt service ratio has been capped at 50 per cent of net salary receipt on non-housing personal loans and 60 per cent on housing loans.       
<br />
					<center><img border="0" src="images/cover_chart_5.jpg" width="396" height="237" align="middle" ></center>
					
<p class="style15" style="line-height: 14px">
<b>The CBO has permitted Islamic banking in Oman. How will this improve the banking industry and benefit customers in Oman? </b>

<br />
<br /> 

With regard to Islamic banking, two new local banks, Bank Nizwa and Al Izz Islamic Bank have been granted approval with the former commencing operations in December 2012. Almost all existing local banks have evinced interest in establishing windows for practicing Islamic banking with most of them already operational. The CBO is confident that the advent of Islamic banking will have a positive impact on the economy. Banks offering Islamic banking products are optimistic that they will be in a position to significantly expand their business which will benefit the economy as a whole. The inclusion of Islamic banking will add to the competitive environment, providing the customers the benefit of choosing between both conventional and Islamic banking products. 
<br />
<br />

How is the Macro-economic situation in Oman and how is it impacting the banking sector? 
During the year 2012, the Omani economy continued the growth momentum aided by high crude oil prices and sustained domestic demand, supported by large public expenditure and accommodative monetary policy pursued by CBO. During January – September 2012, the nominal GDP increased by 13.1 per cent, with GDP emanating from the hydrocarbon sector registering a growth of 11.2 per cent and non-hydrocarbon sector witnessing a growth of 13.2 per cent. The general price level remained under control with the average inflation based on CPI for Oman at 2.9 per cent compared to 4.1 per cent in the previous year. The strong GDP growth and supporting monetary and financial policies had a favourable impact on business growth and performance of commercial banks. With greater participation of commercial banks in the development process together with large investments by the government, the balance sheet of commercial banks are expected to remain healthy. Oman’s saving and investment as proportion to GDP has increased significantly during the recent years. The future of Oman’s banking system remains promising taking into account the pace of economic diversification and the role of the private sector in the development process.&#8194;

