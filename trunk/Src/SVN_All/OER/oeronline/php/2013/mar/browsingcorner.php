				<img border="0" src="images/browsing_corner.jpg" align="right" style="border: 0px solid #C0C0C0; " hspace="12" vspace="3" width="130" height="188"></font><font face="Verdana" size="2"><b>In his Antifragile, Nassim Nicholas Taleb, presents his exceptional theory of domesticating, dominating and conquering the opaque and the uncertain. A review by Muhammed Nafie</b></font><font face="Verdana" size="1"><br>
				<br>
				How instrumental were randomness and uncertainty in the evolution of human civilisation as we see it today? Most of the phenomenal changes in human history were not born out of our planned and meticulous pursuit of a premeditated goal, nor were they the results of our domain expertise and knowledge. In fact we don’t know much about how the future is going to unfold and it has been this ignorance and uncertainty, rather than our certitude and firm conviction, that prompt us to brace ourselves to meet any eventuality head on. Had it not been for the challenges and uncertainties, our latent talent and intellectual acumen could never have been tapped effectively.<br />
				<br>
				In his voluminous Antifragile, Nassim Nicholas Taleb, whose previous title Black Swan was celebrated for its eponymous metaphor, presents his exceptional theory of ‘domesticating, dominating and conquering the opaque and the uncertain’. The book which may occasionally test your patience, especially if you have already read Black Swan and Fooled by Randomness, as the author unnecessarily draws on his bombastic 
				knowledge of philosophy to drive home almost the same theory. Taleb reiterates that in order to be
				 successful one has to make use of randomness and uncertainty, instead of hiding from them. He argues
				  that one really shows one’s mettle under pressure therefore if we want to be the fire, we have to
				   wish for the wind. Taleb observes that a lot of thing benefit from shocks as they thrive and grow 
				   when exposed to volatility and randomness. He coins the word antifragile which he says is beyond 
				   resilience or robustness. <br /><br />               The resilient resists shocks and 
stays the same; while the antifragile gets better.
<br />
Antifragile goes to the extent of stating that absence of political instability, even war, let explosive material and tendencies accumulate under the surface. Absence of fire lets highly inflammable material accumulate. Therefore peace-some kind of forced, constrained, non-natural peace- may be costly in lives. Citing an example for this seemingly nonsensical theory he examines how a century of relative peace in Europe, coupled with the rise of the heavily armed nation-state, triggered the great world wars in the 20th century. However, he believes that war is not a good thing, because as the victim of a brutal civil war in Lebanon, he can attest to its horrors. 

<br /><br />
The book also examines how the idea that systems may need some stress and agitation has been missed by those who grasp it in one area and not in another. Human somehow fails to recognise situations outside the contexts in which they usually learn about them. He reminisces the story of a banker whom he met in the driveway of a hotel in Dubai (described as a pseudo city, maybe because it failed Taleb’s antifragility test) who made a porter carry his luggage and after 25 minutes, lifted free weights at the gym, trying to replicate natural exercise as if he were swinging a suitcase.<br /><br />In short, in 500 plus pages, Antifragile waxes eloquent about the importance of ‘nopredictive’ decision making to counter the uncertainties in business, politics, medicine, and life in general.&#8194; </font></p>

