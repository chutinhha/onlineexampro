				<font face="Verdana" size="1">
				Lamborghini is normally known for its permanent four-wheel drive models: the Gallardo 550-2 refers to the 550 hp power output, and the two stands for the drive to the rear wheels.  But the LP550-2 provides the performance and excitement of Lamborghini but in an emotional two-wheel drive format. The setup is laid out to ensure that the rear end remains reliable and stable at all times, while the front axle steers with the highest level of precision and limits movement to an absolute minimum.<br>
				<br>
			
				<br>
				<b>Performance and sportiness <br>
				</b>In the Gallardo 550-2, the compact and lightweight power unit generates 405 kW (550 hp). At a dry weight of only 1,380 kilograms, this guarantees outstanding performance: the LP 550-2 coupe is catapulted from 0 to 100 km/h (0-62mph) in a breathtaking 3.9 seconds, while the top speed of 320 km/h (199 mph) guarantees its place among the ultimate in super sports cars.<br>
				<br><strong>Driving fun</strong>
               <br />
               In order to shape its unique character that combines driving fun and safety, the engineers at Sant’Agata Bolognese have incorporated extensive modifications into the Gallardo LP 550-2.  It was not simply a question of moving the transmission to the rear axle, the vehicle had to be adjusted and tuned in every element of its driving dynamics. This includes springs, dampers, stabilisers and even the tyres, with aerodynamic modifications too.
