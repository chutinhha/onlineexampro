                    <hr color="#C0C0C0" size="1">
					<p class="style15" style="line-height: 14px">
					<font size="2" color="#333333"><b>Real issues untouched<br>
					</b></font>
					Most of the industrialists are of the opinion that the authorities are penalising everyone for the mistakes of errant companies by generalising the rules across the economic sectors. Moreover, by increasing minimum wages to a higher level compared to other countries, the country is encouraging the rise of an uneducated class in society. Says Hussain, “We have to understand that various people from different backgrounds, and in some segments education may not be a priority. A minimum salary assurance may deter certain parents from imparting education to their children for 18 years to get paid RO600, compared to a choice of earning RO350 from day one.”<br>
					<br>
					“We are not touching the major critical issues. Why are Omanis reluctant to work for Omani companies? Can we place our finger on what is a motivating salary for an Omani? If yes, can the private sector companies pay this and survive? What is the output of the Omani individual as compared to an expatriate? All these issues need to be discussed and documented and a decision should be taken based on these conclusions,” Tawfeeq points out. “There is a need for a coordinated approach as most government agencies like the OCCI, PEIE, Ministry of Manpower and the Ministry of Commerce and Industry are all working in silos presently,” adds Tawfeeq.
		<br />
		<br />
		Continuing further, he observes that there is no taskforce to oversee the consequential effects of government decisions, as the overriding objective seems to be to hire Omanis and to pay them more, without looking at the ramifications. As a result there will be companies which will close down, because of their inability to pay more, which in turn will lead to more Omanis looking for jobs.
Everyone knows the government risks courting a social problem if there are thousands of unemployed and dissatisfied youth, but hiking minimum salaries to unaffordable levels is not a panacea for socio-economic problems. Amidst all this, it seems that it is better to agree that the glass is both half full and half empty and it is better to ensure that the glass gets topped up without breaking it!&#8194;
		
