					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>Spiralling effect<br>
					</b></font>
					<img border="0" src="images/cover_6.jpg" align="right" hspace="4" vspace="4" width="200" height="176">About the repercussions of the hike in minimum wages, HE Tawfeeq Abdulhussain Juma al Lawati, representative of Muttrah at Majlis A’Shura, says that the other thing which he has heard is that the new recruits will start from RO350. If the senior people who have been working in an organisation, get a hike to RO325, but a new appointee gets RO350, then the existing ones will resign and get a new job or demand parity. According to him, there are different segments in the economy while some companies are more mature and can easily absorb the hike in minimum wages, SMEs are the ones which are going to be hit badly and they might not be able to afford this kind of salary and may eventually run out of business. There is a standing Shura Council formula to hike the minimum wages, which  would minimise the impact on the private sector (see box ‘The Majlis A’Shura formula for private sector’).<br>
					<br>
					
“With the cost of manpower going up, it is only natural that there will be a cascading effect on the price of goods, but the consumer protection authority will not allow this. I agree that there should not be a price increase across the board, but there are segments which are technology-intensive and others which are labour-intensive like the services sector. We need to review and see what is the impact or the authority should do a product-by-product research and see where the prices can be increased,” adds HE Tawfeeq.
<br>
					<br>
					Hassan Mohammed Juma, managing director, Mohammed Juma Sultan Company says that as a company, they suffer only on the construction side due to the recent hike. “I can recruit Omanis but they are not willing to work, so the government has to waive off certain things taking into consideration the cultural mores of the country. We are paying above the minimum wages in the rest of the group. But in construction we are hit because expatriates are coming in for lesser wages, so the cost parameters are not right. Moreover, it’s all about inflation. If I am going to get a better rate for my contract, it is fine. Construction should not be put in the same bracket as other industries because of the unwillingness of Omanis to take up jobs in this sector. I am willing to take, even if they do 40 per cent of the job, but we struggle to get enough people. In construction, a RO325-350 salary is not feasible at all.”</td>
