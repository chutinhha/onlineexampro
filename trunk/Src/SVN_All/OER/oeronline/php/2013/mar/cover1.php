                    <hr color="#C0C0C0" size="1">
					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>Live with it<br>
					</b></font>
					<img border="0" src="images/cover_2.jpg" align="right" hspace="4" vspace="4" width="200" height="176">The impact of the hike in minimum wages would be very high unless the salary hike is really subsidised by the government, says Bhaskar Dutta, CEO, Al Jazeera Steel. Continuing further, he says that without any vocational training, a school leaving pupil from class 10-12 with no job knowledge, getting this high a salary is not appropriate. When asked if the new wage structure is a reality with which companies will have to live with, Dutta says, if the SMEs are not subsidised, this wage burden in the competitive market condition will push a lot of SMEs to close down in the near future.<br>
					<br>
					Hilal bin Hamad al Ahsani, CEO, PEIE, counters by saying that in any industrial development, there are positive and negative impacts. What do we have to do? In America, Europe and Far East markets, wages are always fluctuating; but here at least the minimum wage is fixed. We need to apply efficiency and also cost reductions. <br>
					<br>
					One has to analyse each of the elements contributing to production cost in the industry and you can see that not many of these industries will be affected. Only the really small industries will be impacted in the short run, but in the long run, they can revisit and shift their cost analysis and strategy. Yes, there will be some sort of decline, but this will be limited to the short term.</td>
