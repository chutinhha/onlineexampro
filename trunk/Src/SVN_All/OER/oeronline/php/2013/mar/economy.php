<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt"><br />With government spending being buoyed by an oil revenue surplus in 2012, the authorities are turning their attentions to the thorny issue of job creation. And, with production widely anticipated to decline later this decade, the government is increasingly looking to stimulate the private sector as the key to developing the non-oil economy.<br /><br />
On the back of these strong revenue streams, the Sultanate’s 2013 budget has raised spending by nearly 30 per cent over its 2012 plan, to $33.5bn. Sectors including education, health, housing and social welfare have all enjoyed increased levels of spending – in some cases a 22 per cent rise, according to Ministry of Finance statistics. Moreover, with the price of oil continuing to stay some $25 above the $85 per barrel figure the budget was worked out on, it looks likely these sectors, and the broader economy, will continue to benefit from strong support.
<br /><br />Yet, another area has been singled out to benefit from increased government funds. One of the key tenets of the 2013 budget is His Majesty Sultan Qaboos bin Said’s establishment of a new $182mn fund to help citizens set up small businesses. Furthermore, the initial capital of the fund is set to be increased by $18mn each year. Small and medium-sized enterprises (SMEs) will also be given state land for free as part of a package of ‘head start’ incentives for new business.
<br /><br />The goals of this spending are clear: to create jobs through the development of the private sector, something that has proven to be a challenge in the past. Government figures indicate that the number of registered unemployed was reduced to about 17,000 in 2012, according to the Ministry of Manpower. However, the fact remains that unemployment amongst nationals is still higher than the authorities would like. Of course, small business has long been seen as having huge untapped potential, but just how realistic is this focus on entrepreneurship in terms of the level of job creation necessary? 
<br /><br />Assessing how effective government investment and incentives will be in terms of creating jobs and developing the private sector will take time, but there is already some evidence that the numbers are heading in the right direction. In January, Minister Responsible for Financial Affairs, Darwish Al Balushi told local press that government spending had created 36,000 private sector jobs for nationals in 2012, and that he anticipated a further 56,000 would be created this year.
<br /><br />However, it is far from being a cure-all, and challenges remain. Firstly, some have called the basic premise of the strategy into question, pointing toward certain structural issues within Oman’s SME sector. According to Qais Al Khonji, owner of Genesis International, based in Muscat, the small size of the market and lack of proper training hamper most start-up ventures from turning into fully-fledged businesses. A second major challenge relates to work attitudes; nationals prefer public sector jobs, and even successful SMEs face problems recruiting Omanis.<br /><br />
While this will inevitably cost private businesses and has been criticised in 
certain quarters, it could go a long way toward challenging the commonly-held 
negative views about working in the private sector. It is not that there is a 
shortage of jobs in the private sector; rather, Omanis have historically sought 
more highly-paid public sector positions. This is arguably the most significant 
structural challenge to private sector growth.<br /><br />Other issues are being addressed 
too. For example, steps are being taken to tackle the issue of a lack of 
technical knowledge and know-how among would-be entrepreneurs. In December last 
year, the Public Establishment for Industrial Estates (PEIE) announced the 
establishment of a National Business Centre to begin operations by March 2013. 
The centre will act as a platform providing facilities and value-added services 
to support the growth of entrepreneurial skills through training, coaching and 
mentoring.<br /><br /> And, whilst job creation is a priority, there are other benefits from 
more SMEs and the development of the broader private sector – the success of the 
government’s prioritisation of SMEs has come in the form of increased interest 
from foreign companies, looking to establish contracts with local businesses. In 
February, for example, Brazilian miner Vale announced it had awarded eight 
contracts worth over $2.5mn to local SMEs, as the company looks to develop a 
sustainable supply chain and stimulate the growth of SMEs in the Batinah 
governorate. Such developments will help to create virtuous cycles of growth and 
reinforce the credentials of the private sector, raising the profile of Omani 
SMEs abroad.<br /><br />This is precisely the kind of success story that the authorities 
will be hoping will drive the future growth of the Sultanate’s SMEs, indicating 
that despite the challenges, the investment is already bearing fruit and has the 
capacity to not only create jobs, but play an important role in the long-term 
sustainability of the economy.&#8194;<br /
Yet given the bumper oil returns last year, the government is well placed to tackle these issues, and is showing that it is prepared to do more to stimulate the private sector than ever before. Arguably the biggest issue that has long turned Omanis away from the private sector is the lower average pay scale relative to the public sector. In a bid to tackle this, it was announced in February that the monthly minimum wage for nationals employed in the private sector will be increased to RO325 – constituting a rise of more than 60 per cent – with effect from 1 July 2013. The increased minimum wage is likely to affect 122,000 of the 172,000 Omanis working in the private sector, according to the government.
Changing perceptions 
While this will inevitably cost private businesses and has been criticised in certain quarters, it could go a long way toward challenging the commonly-held negative views about working in the private sector. It is not that there is a shortage of jobs in the private sector; rather, Omanis have historically sought more highly-paid public sector positions. This is arguably the most significant structural challenge to private sector growth.
Other issues are being addressed too. For example, steps are being taken to tackle the issue of a lack of technical knowledge and know-how among would-be entrepreneurs. In December last year, the Public Establishment for Industrial Estates (PEIE) announced the establishment of a National Business Centre to begin operations by March 2013. The centre will act as a platform providing facilities and value-added services to support the growth of entrepreneurial skills through training, coaching and mentoring.
And, whilst job creation is a priority, there are other benefits from more SMEs and the development of the broader private sector – the success of the government’s prioritisation of SMEs has come in the form of increased interest from foreign companies, looking to establish contracts with local businesses. In February, for example, Brazilian miner Vale announced it had awarded eight contracts worth over $2.5mn to local SMEs, as the company looks to develop a sustainable supply chain and stimulate the growth of SMEs in the Batinah governorate. Such developments will help to create virtuous cycles of growth and reinforce the credentials of the private sector, raising the profile of Omani SMEs abroad.
