						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Need for 
						diversified options</font></b></p><br /><br />
						<p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<img src="images/hospitality.jpg" width="232" height="323" border="0" align="left" vspace="1" hspace="3">
						Srinith De Silva, Chief Operating Officer, Oman Hotels 
						shares his thoughts on the potential and challenges of 
						the hospitality industry in the Sultanate with Mayank 
						Singh<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
<b>				You have joined Oman Hotels recently. What are your first thoughts on Oman and its potential as a tourism destination?
</b>
My first impression of Oman is that it is a country with a great potential for hospitality. More than the product it is the people. I have worked in Middle East region and the moment  you land at Muscat airport, you can feel the warmth. In any country the challenge would be to find people who suit hospitality, but in the case of Oman, the people are friendly and this is ideal for hospitality. As a group we have two properties in Muscat – Al Falaj Hotel and Ruwi Hotel, Sur Plaza hotel in Sur and the Desert Night camp and the Al Wadi Hotel in Sohar. All these properties have a lot to offer and have a lot of potential. The leisure market was the best in 2008 and in 2012 we saw a bit of that year coming back as things were picking up, tour operators who were not operating after 2008, have started coming in.<br /><br />
<b>How do you define the leisure market?</b>
<br /><br />

The leisure market comprises people who come on a round trip, it may involve spending three nights in Muscat, then they go to Sur, do turtle watching, go to the desert and then they come back to Muscat and then they depart. The leisure market consists of groups, and these tourists come from France, Germany, UK and Italy – which are our primary markets. In 2012, the Italian and French arrivals grew strongly and looking at their numbers 2013 may well prove to be one of our best years.
<br />
				</font>
						<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><b>What kind of occupancy have 
				your hotels been enjoying? The hotels business in the region 
				gets affected by Ramadan and the peak summer months, given this 
				how do you make sure that occupancies do not fall drastically 
				during these months?</b><br /><br />
				In 2012 we did average occupancies of around 75 per cent. During Ramadan and the second Eid, we do various promotions like an early bird offer, advanced purchase etc. We know that it is going the be a quite period, so we float special rates in the market and this helps us to fill up. The difference between a peak season rate and low season would be around 30 per cent and this attracts customers. We do special promotions for individual hotels too, for example for Desert Nights we are closed in the months of June and July as it is too hot, but we have already started taking bookings for August 2013. Before we get close to August we want to sell 20 rooms per day. For example we are telling people that if you make a booking now, you will get a special rate on extra activities like dune bashing, quad biking options and various other things.<br />
				
				<br />
				The summer months in 2012 were actually very good for us because a number of projects were going on in and around Sohar and this helped Al Wadi Hotel, as we had a lot of corporate clients. Sur had a lot of ministry bookings and that clientele came to us. In Muscat, we had a similar experience and Desert Nights had leisure groups coming in good numbers. So, it is not reasonable to predict how there months would pan out. Our business is based on corporate and government (ministry) bookings and in 2012, this was amazingly good. With the economy moving, we benefit from the overall growth of the country. In Sohar around 13 major projects are underway and we are completely sold out and in some cases we are hiring apartments outside the hotel to accommodate guests.
				<br /><br /><b>A commonly heard refrain is that Oman does not have enough four star hotel rooms where business travellers can stay. Do you agree with this?</b>
<br />
<br />
That is true; there should be a mix of hotels across categories. Oman has been marketing itself as a high-luxury destination, but I am not sure whether this is the correct way of marketing the country, because from a tourism point of view you require not just top-end clients but a mix of different class of tourists, as this feeds three and four star hotels. Looking at the business there is great potential to add to four star hotels and these should be unique. In India, for example, the Tourism Board promotes heritage and boutique hotels etc giving tourists stay options apart from star hotels and this can be replicated in Oman.

<br />
<br />

<b>The Ministry of Tourism wants to promote Oman as a high-end destination; do you feel that this approach needs to be changed?
</b>				
<br />
<br />
If you look at the leisure market anywhere in the world, by its nature it is not high-end, because the leisure market primarily comprises of tourist groups. If you are coming in as an individual traveller with a family, then you are a high-end tourist, but they are not enough to sustain the tourism business, because there is a limitation on how very many well-off people will visit Oman. Our main business comprises groups. If a group comes the probability is that they will go through a package, which includes breakfast and dinner in addition they may spend on that odd drink and mineral water, but they are unlikely to spend thousands of rials. If we send out a message that Oman is a very high-end destination, this may deter tourists who may be thinking of visiting the Sultanate, as they may presume that it an unaffordable place. So this works as a disadvantage for the country.
<br />
<br />

<b>There is a feeling that the average room rates (ARR) in Oman or was less than the realisation per room in other countries of the region. Do you agree with this argument?</b>

<br />
<br />
ARRs in Oman have been the same in the last two years, but it differs from hotel to hotel. Dubai can command better rates because it attracts so many tourists, but in Oman we are still not in a position to demand higher room rates leading to stagnant ARRs and I think it would be the same in 2013. We can improve ARRs if the industry comes together and raises rates in tandem. Otherwise, we will have to wait till the new airport is ready and the requisite tourist infrastructure is in place, and then we can demand better rates.

<br />
<br />


<b>The government has big plans for tourism and it is looking at attracting a large number of tourists into Oman. Given this aspiration, does the country have enough hotels rooms to accommodate such an influx? </b>

<br />
<br />

No there are not enough hotel rooms to accommodate future demand. Secondly, if we need more hotels then work on them needs to start now itself. The tourism infrastructure in Muscat is adequate but when I talk about adding rooms, it is not just for Muscat, but we need to look at various places like Nizwa which presently has only a couple of quality hotels. Similarly, Jebel Akhdar, Duqm etc have great potential, but these places lack good accommodation options. Tourism has been affected by the global financial crisis and therefore private investment has been slow in coming in but things are picking up. We hear about international players like Best Westin, Kempenski and others coming into the country and I am sure that more will follow. 

<br />
<br />

<b>Atß Oman Hotels are you looking at expanding your room count or upgrading your offerings?</b>

<br />
<br />

As a company we are looking at expanding our presence in future. As of now we have gone in for an extensive refurbishment across all our hotels. At Al Falaj we have completely refurbished our rooms and public areas in 2011-12. A similar exercise has been carried out in Sur Plaza and Al Wadi. We have invested a lot of money in upgrading our offerings across hotels, and as a result we were able to pull various leisure groups that were not coming to us. We have also invested heavily on the back of the house operations like staff quarters, staff recreation, improving skills with training courses etc which we believe is the heart of the operation.&#8194;
				
				
				
