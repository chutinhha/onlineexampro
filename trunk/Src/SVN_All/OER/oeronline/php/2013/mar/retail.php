<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" size="1"><br>
Swiss luxury watchmaker Rolex conducted a training session for the team members of Khimji’s Watches, the host to these world’s finest time machines in Oman, on February 23-24 at the Grand Hyatt. The two-day training programme was conducted by Thierry Schroeter, sales trainer at Rolex, Switzerland. Schroeter not only trains salespersons across the globe on the latest sales techniques but is equally adept at training the teams on project management skills. His previous tenure as a media analyst at Rolex helps him bring to the table insights on media management and public relations as well.
<br />
<br />
The training programme was attended by the sales team and senior management of Khimji’s Watches. Anil Khimji and Pankaj Khimji, directors, Khimji Ramdas; Vivek Pande, CEO, Luxury and Lifestyle Group; Madhursinh Jesrani, general manager, Khimji’s Watches and Rajive Ahuja, head of Corporate Communications, were some of the key attendees.
<br />
<br />
The agenda of the session covered a host of different topics including the heritage of brand Rolex, its manufacturing process, patents and exclusive features, technical features, POS guidelines, World Service etc. 
<br />
<br />
Madhusinh Jesrani commented, “We are thankful to Rolex and Thierry Schroeter for having conducted this highly informative session for our staff. Schroeter is well-known for training Rolex salespersons worldwide and for project management within the sales training department. With his rich knowledge and global experience, he has surely added value and enhanced the skills of our human resources. The outcome has been satisfactory and heartening. Many of our employees are more motivated and geared up to perform even better and scale greater heights”.
<br />
<br />
“At Khimji’s Watches, we believe that our employees are our key resources and we will keep providing them with such training courses to inform, empower and encourage them to excel in their duties and offer our customers the best quality services”, Jesrani added.
<br />
<br />
Set up in 1973, Khimji’s Watches brings some of the most exclusive and cherished timekeeping brands such as Rolex, Chopard, Cartier, Piaget, Oris, Girard Perregaux, Caran d’Ache, Mikimoto, Bell & Ross, Frederique Constant and Vertu together under one roof. Khimji’s watches does not just sell these watches, but also offers an in-house service workshop adhering to rigorous international standards, ensuring that watches are repaired in Oman itself, without having to be sent anywhere else.&#8194;
