<b>In what ways will this programme help Rolex (Khimji’s Watches) employees to serve their customers better?
</b>				
<br /><br />
Rolex sales training will drive significant improvement in the performance of our sales force by addressing the selling skills, technical know-how of the product, communications practices and professional attitude of every team member participating.
<br /><br />
<b>Does your staff undergo such training programmes regularly? </b>
<br /><br />
We do have training programmes conducted by different brands for our sales team to build up their confidence level.
<br /><br />
<b>How often are such training programmes conducted?
</b>
<br /><br />
Rolex is a front runner in this market and our staff has been undergoing orientation programmes with Rolex on a one -to-one basis for many years now. We are on a continual training mode with our principals; but this is the first time that we are having a large programme where all our staff will be guided through a two-day focused session. This will help in formalising the training modules which were conducted more on an individual basis.
