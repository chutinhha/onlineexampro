					This new directive caught most of the private sector offguard and cautious reactions like ‘we are surprised but it is inevitable if you want to improve the quality of life of Omanis,’ etc began to do the rounds. On the other hand, corporate captains also spoke about the effect of the salary hikes on the cost of products and services, saying that the prices of commodities could go up, inflation would rise, FDI would slow down etc. Extreme scenarios like the economy taking a turn for the worse, also began to be floated. Some observers professed that that such a hike would create new social challenges, rather than solving old ones.<br>
					<br>
					Talking about the timing of the decision to hike the minimum wages, Dr Salah bin Hilal al Maawali, director general, SME Department, Ministry of Commerce and Industry, says that many studies were conducted and meetings with relevant stakeholders were held before taking the decision to hike minimum wages. He believes that the grace period (till July 2013) is enough for the enterprises to settle their conditions and acclimatise themselves to the effects of the decision. (See full interview: ‘Benefits for all from raising minimum wages’).
					<br />
					<br />
					HE Maawali also says that a number of sops are being offered to soften the impact of the salary hike on SMEs. Dedication of 10 per cent of the projects offered by the Tender Board to SMES and making it mandatory for major companies to grant 10 per cent of their contracts to SMEs are just few examples of the support provided to this sector.</td>
