                    <hr color="#C0C0C0" size="1">
					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>Too complex a topic<br>
					</b></font>
					<img border="0" src="images/cover.jpg" align="right" hspace="4" vspace="4" width="200" height="176">The SME topic is much more complex than it was previously thought of, says Khalid M Al Zubair, managing director, The Zubair Corporation and chairman of the SME Committee of the Oman Chamber of Commerce and Industry – one of the organisations which helped turn the spotlight on SMEs. The challenges for SMEs were summarised as – the lack of finance and stifling government regulations. But actually there are four different pillars that need to be addressed. Apart from the two mentioned above, the other two are as important and one of them is cultural in nature. Oman has become a risk-averse society enfeebling entrepreneurship. The fourth one is building capacity.
To bring about a seminal change in attitude and skills would require community involvement and going down to schools and imparting the necessary skills. Doing this, would help in building a strong SME base and equip Omanis to join the private sector.
<br>
					<br>
					In addition, there is a need to educate government representatives about SMEs who come about seeking loans. Does the government representative realise that if he/she delays the disbursement of the funds to the SME entrepreneur by one month, the overhead costs for that person will increase and his business will fail and consequently his family and he will suffer? <br>
					<br>
As far as capacity building is concerned, it was realised that just getting a loan is not enough, as without an SME incubator or mentor, 80 per cent of start-ups were destined to fail. These mentors are people from society who devote their time and effort to helping start-ups, by adopting them. <br>
					<br>
					Overall, the symposium has been a success, says Khalid. One of the objectives of the symposium was to highlight the importance of SMEs as a topic of national importance. Secondly, it also ameliorated concerns about a conflict between SMEs and large companies. “We want the large companies to contribute to the SMEs and not just as part of their CSR. The big companies should focus on their competencies and create an ecosystem for the development of SMEs,” Khalid adds.</td>
