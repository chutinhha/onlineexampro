						THE SMALL AND MEDIUM ENIGMA</font></b><p class="style15">
				<span class="style14"><strong><font size="2">The symposium on the development of small and medium enterprises, which proved to be a landmark event, reviewed the success stories of entrepreneurs in different sectors, suggested legislations and revised policies to create an environment conducive for the growth of SMEs. But the announcement on the hike in minimum wages has left the private sector bewildered. Visvas Paul D Karra speaks to various industry captains to take stock of the current situation</font></strong></span><p class="style15" style="line-height: 14px">
						<p class="style15" style="line-height: 14px">
												Bahla, known for its famous UNESCO-listed heritage fort, was in the days of yore also well known for its pottery, which was in great demand. It’s not a coincidence then that circa 2013 it goes down in the history of modern Oman as the place where the foundation was laid for the development of the micro, small and medium industries, commonly known as the SMEs.
						<br /><br />In response to the Royal Directives of His Majesty Sultan Qaboos bin Said, the symposium on the development of small and medium enterprises (SMEs) was organised at Saih Al Shamikhat in the Wilayat of Bahla, Governorate of Al Dakhiliyah. It was a watershed event as the symposium sought to sort out the SME conundrum by bringing all the relevant stakeholders from the public and private sector under one common tent, literally and figuratively.
						<br /><br />Issues such as instilling a culture of entrepreneurship in Omani youth, enhancing efforts to link education to the labour market requirements, studying obstacles facing the development of the SME sector and proposing solutions, empowerment of women by encouraging them to set up their own private businesses, developing skills of entrepreneurship and preparing them to become self-reliant were the myriad matters that were discussed threadbare.
All the issues mentioned above fell into four broad themes:
<br /><br /><i>• Entrepreneurship culture<br>
						• Available SME opportunities and support programmes<br>
						• Polices, laws and procedures for the sector<br>
						• Financing and investment of private ventures</i><br>
<br />
<br />
The importance of this event underscored the growing interest in SMEs in the country as an important means for not just diversifying the economy but also viewing this much neglected industry as the next big economic growth engine for the Sultanate. The end result of these efforts would be new job opportunities, an increase in the spirit of innovation and the application of technology to business plans, which in turn would create products and services benefiting the country. A side benefit of this would be a reduced dependence on imported labour, reduction in the expatriate population and plugging the drain of rials as foreign exchange.
<br />
<br />
A set of recommendations (see box ‘Recommendations to be implemented’) were issued for immediate implementation in the wake of the conference, evoking a positive response from potential entrepreneurs. An SME support fund known as the Al Rafd Fund (see box ‘Al Rafd Fund’) was also established to provide much-needed finance to new SMEs. A number of companies expressed their interest to help SMEs with a flurry of public announcements, leading to all-round cheer.
Bolstering the movement was His Majesty Sultan Qaboos bin Said’s speech affirming that the national economy of any country is built on SMEs, while adding that most industries started humble and then grew onto become major industries in various parts of the world. 

<br />
<br />

Everyone was excited at the turn of events. And despite the clichéd development route being adopted, the story would have continued to play out, had it not been for a sudden twist in the tale. In less than a week following the symposium, the government announced a new regulation stipulating the minimum wages for existing Omani employees to be raised to RO325 while fresh recruits would have their paychecks written for RO350 straightaway, effective from the second half of this year.
