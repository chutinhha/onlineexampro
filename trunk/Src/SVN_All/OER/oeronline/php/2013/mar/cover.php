					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>There is a link<br>
					</b></font>
					<img border="0" src="images/cover_2.jpg" align="right" hspace="4" vspace="4" width="200" height="176">So is there a link between minimum wage hike, SME development and the large corporates? According to Khalid, the link exists between the two when you look at it from a holistic point of view, although the minimum wage hike was not part of the SME symposium.<br>
					<br>
					First of all, there has to be a differentiation between an entrepreneur who is following the rules and regulations and a businessman who is doing things like hidden trading. Says Khalid, “If my business were an SME, I will suffer from the increase in minimum wages. But as the government has committed to give out projects worth millions of rials to SMEs, as a part of its new directives to distribute 10 per cent of projects for SMEs and if these are given to genuine SME entrepreneurs, then paying RO350 salary should not be an issue. The government must identify genuine entrepreneurs by asking – is he an owner whose bread and butter comes from being an SME? Is it a company which employs Omanis? Is he a committed entrepreneur? Having done their due diligence, projects should be handed to committed start-ups. What happens then is that the difference in wage becomes a positive action.”<br>
					<br>
					According to Khalid, the economy needs to grow to create new jobs for Omanis and to give them higher salaries. You need to link all the incentives to law abiding businesses. “If we talk about making regulations easier, we should make it easier for those who are following regulations. That way we can bring about healthy solutions. It keeps the money in the country and creates purchasing power. There also needs to be a labour law that applies to the workers as well, saying that if you are not productive, then the law will be fair. This is an integrated package which will make the raise in salaries feasible,” adds Khalid.
Khalid says that we should stress on in-country value. “How many projects does the large contractor give to SMEs? Why should we import when we can source them locally?” he asks. So the government is obliged to make this plan collectively work better for the private sector, otherwise we will not be a competitive market and economy and whatever jobs we have today will be gradually lost because at the end of the day, we don’t decide the salaries but it is the market.

<br />
<br />

Explaining this, Khalid says, “We don’t decide the production levels in our company but it depends upon market demands. So, it’s the market that gives us work through which we generate income and pay our employees. Tomorrow, if prices go up and I cannot sell my product, first I will trim down and then finally close down.”
