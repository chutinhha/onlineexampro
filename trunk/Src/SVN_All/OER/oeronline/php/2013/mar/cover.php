					<p class="style15" style="line-height: 14px"><b>
					<font size="2" color="#333333">Salary hike is fine but…</font><br>
					</b><br />
					
					<img border="0" src="images/cover_3.jpg" align="right" hspace="4" vspace="4" width="200" height="176">There are others who feel that a mere hike in salaries is not good enough to weed out deep-rooted problems. Moreover, it should not be misused as a bargaining tool by society or the authorities.<br>
					<br>
				Hussain Salman, Al Lawati, vice chairman and managing director, Oman Cables Industry says that first of all the terminology for a minimum salary should be established cautiously as Oman is a free market economy and random changes will have far reaching consequences on the entire investment environment.
“I don’t think there is anybody in Oman who doesn’t want a salary rise for his own people,” states Hussain emphatically. But we are a part of the global economic map, wherein every country is trying to market its potential to future investors. So there are four aspects to attracting investments: The first is market potential or advantage, second is skilled manpower, third political stability and fourth is the cost of operations. If we look at the second aspect namely manpower, do we have skilled manpower? Can we create a base of skilled manpower by paying someone RO325 or 525 or even 1,025 rials. Skills come from the eagerness of a society and individual to learn, and from thethe quality of schools and training institutes. It is the combination of a series of factors, which create a generation of people who understand the seriousness of work and know that nothing comes for free in life. One has to work hard to aspire to a better quality of life.” 
<br>
					<br>
					Talking about the availability of skilled manpower at a seminar on – ‘Uplifting Local Workforce Skills,’ – organised by the Public Establishment for Industrial Estates (PEIE), Mohammed al Kharusi, director, corporate operations, MB Holding, says, “we do face a problem of skilled manpower. When we get some people and train them, somebody poaches on him immediately. The industry is suffering because of the lack of supply. It’s a big problem, especially in the oil and gas industry. You pay for the training but the other entity is not paying, however, they will grab a trained person. If the company is really large then it is fine, but what about SMEs? It is very difficult for them.”</td>
