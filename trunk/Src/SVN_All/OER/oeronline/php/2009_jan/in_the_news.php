            <b>Muneera Bint Saif Al Nabhani won the cash award of RO5000 for her 
            business proposal at this year’s Intilaaqah Best Young Entrepreneur 
            Awards<br>
            </b>
            <br>
            <img border="0" src="news3.jpg" align="left" hspace="8" vspace="6" width="350" height="207">The Shell Intilaaqah Best Young Entrepreneur Awards ceremony was 
            held recently at Crown Plaza. The awards were presented by HE 
            Engineer Ali Bin Masoud Al Sunaidy, Minister of Sports. The prizes 
            included a cash award of RO5,000, was given away with a trophy and 
            citation at the ceremony. Mohammed Al Mahrooqi and Khalifa Al 
            Kuwaitri were selected the second and third prize winners with a 
            cash award of RO3,000 and RO2,000, respectively. The jury comprising 
            prominent individuals evaluated the nominees. Andrew Wood, Shell 
            Country Chairman, who was also present on the occasion, said: “Often 
            young people come up with ideas, but in many cases they lack the 
            expertise and skills to turn these ideas into a profitable business. 
            Shell through the Intilaaqah Programme has helped many Omanis to set 
            up their own business.”<br>
            <br>
            “We have trained 6,093 Omani nationals, and based on our statistics, 
            more than 40 per cent of them still manage their own businesses,” he 
            said. More than 170 persons submitted their proposals, said Abdullah 
            Bin Hamoud Al Jufaili, Director of Intilaaqah programme. The 
            campaign in collaboration with Oman Mobile and DHL contributed to 
            the success of the competition. Since 1995, Intilaaqah has helped 
            many Omanis develop their businesses. The purpose of Intilaaqah 
            award is to encourage and provide assistance to Omanis to start 
            their own business. This is done by imparting training with free 
            services that range from business counseling to business planning 
            and ownership. The Young Entrepreneur of the Year award aims to 
            honour young entrepreneurs in Oman.</font></p><hr width="90%" size="1">
