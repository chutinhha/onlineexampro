            <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b>HM’s Cup for Best Factories of 2008 presented<br>
            </b>
            <br>
            <img border="0" src="news2.jpg" align="right" hspace="6" vspace="4" width="350" height="250">His Highness Sayyid Hamad bin Thuwaini bin Shihab al Said presided 
            over the annual function, organised by the Ministry of Commerce and 
            Industry, to present His Majesty’s Cup for the Best Five Factories 
            2008.<br>
            <br>
            Five winners of His Majesty’s Cup for the Best Five Factories in 
            2008 and five winners of the Commerce and Industry Ministry Shield 
            were honoured at the function. Oman Fiber Optics, United Dates 
            Factory Company, National Heaters Industries Company, Reem Batteries 
            and Power Appliances Company and Arabian Industries won the Cup.<br>
            <br>
            HE Maqbool Bin Ali Bin Sultan, Minister of commerce and industry, HE 
            Khalil Abdullah Al Khonji, chairman, Oman Chamber of Commerce and 
            Industry (OCCI), and other dignitaries were present at the function 
            held at Al Bustan Palace Hotel.<br>
            <br>
            “This annual competition for honouring Omani companies shows the 
            keen interest of His Majesty in the development of industry in the 
            Sultanate. These awards will be helpful in supporting the indigenous 
            industries and creating more opportunities for the Omanis,” said His 
            Highness Sayyid Hamad bin Thuwaini bin Shihab al Said<br>
            <br>
            Oman Chlorine, Amiantit Oman, Sohar Gases, Future Pipe Industries 
            and Al Waqia Shoes were the winners of the Shields, while Majan 
            Glass Company, Salalah Mills Company and Gulf Stone Company won the 
            certificate for achieving good results in value addition, export and 
            quality. Oman Cables Industry won the shield for excellence in a new 
            category which was introduced in the competition for factories that 
            have won the cup for more than five times. <br>
            <br>
            The factories were divided into two categories: The first section 
            included companies whose investment exceeded RO3mn. The second 
            section consisted of companies whose investment was below RO3mn.
