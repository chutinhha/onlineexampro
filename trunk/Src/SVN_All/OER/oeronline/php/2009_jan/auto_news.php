				<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2">THE 
                2008 MOTORSHOW</font><font face="Verdana" size="2" color="#CC3300"><br>
                </font></b>
					<font face="Verdana" size="1">
					<b>This year, most of the major manufacturers were present and 
                many had something new to demonstrate at the Motorshow. Audi had 
                cars like the R8 and the new RS6 stealing the attention of most 
                of the crowd, Mitsubishi had their new Evolution X and Eclipse 
                and Lexus had all their regular models along with the LS600h. 
                Meanwhile, Toyota showed of its new Sequoia and Peugeot had its 
                new 207RC Special Edition taking centre stage while Mazda 
                debuted its new model the Mazda 6 Ultima. Also present at the 
                show were the motorcycles from Polaris, Victory and Yamaha. 
                Other brands included Seat, Chrysler, Jeep, Daihatsu, Skoda and 
                Great Wall. There were also companies such as V-Cool and Bosch 
                occupying the smaller stalls on the perimeter of the show floor. 
                Quite a few cars made their first appearances at the show, 
                heralding their arrival to the Omani market. Of these here are a 
                compiled list of eight new cars at the show.</b><br>
&nbsp;</font><p align="justify" style="margin: 3px 10px">
					<b><font size="2" color="#CC3300">
                    <font face="Verdana">AUDI R8</font></font></b><font face="Verdana" size="1"><br>
                    <img border="0" src="auto1.jpg" align="left" hspace="8" vspace="6" width="225" height="170">With its high-performance sports car, the R8, Audi took the 
                    lead immediately in the most demanding automobile market 
                    segment. The engine, quattro permanent all-wheel drive and 
                    the body built according to the Audi Space Frame principle 
                    secured the R8 an outstanding position. The R8’s V8 engine 
                    is not only compact but also has no difficulty at all in 
                    soaring up the rev band to speeds as high as 8,250 rpm. The 
                    reward for this is an output of 420hp and a maximum torque 
                    of 430 Nm (317.15 lb-ft) between 4,500 and 6,000 rpm, which 
                    in turn guarantees an immense flow of power. Performance on 
                    the road is correspondingly impressive; the R8 rockets from 
                    0 to 100 kmph (62.14 mph) in just 4.6 seconds, whether with 
                    manual transmission or with R tronic sequential gearshift. 
                    The needle hits 200kmph after just 14.9 seconds, and the 
                    engine’s thrust is only equalled by rolling resistance and 
                    aerodynamic drag at 301 kmph (187.03 mph). The V8 produces 
                    an excitingly full and differentiated sound at all times.
                    <br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					<br>
					</font><b><font size="2" color="#CC3300">
                    <font face="Verdana">MITSUBISHI EVOLUTION X<br>
                    </font></font></b>
					<font face="Verdana" size="1">
					One of the first cars you saw when you entered the showroom 
                    floor was the Lancer Evolution X, a high-performance 4WD 
                    sedan, the newest of the company’s legendary vehicles. It 
                    features a new platform, engine, exterior and interior 
                    designs. The development goal for the new Lancer Evolution X 
                    was to make the car as fast as possible, while also making 
                    it as safe to drive as possible. To that end Lancer 
                    Evolution X is loaded with the very latest in automotive 
                    engineering technology, including the S-AWC (Super All Wheel 
                    Control) vehicle dynamics control system and the newly 
                    developed, power-efficient Twin Clutch SST (Sport Shift 
                    Transmission). Lancer Evolution X is powered by a new 
                    turbocharged engine that adds a high-performance 
                    turbocharger to the 2.0-litre 4-cylinder 16-valve DOHC MIVEC 
                    unit. Developing more power over the full rev range, this 
                    engine generates more torque at 422 Nm@3500 rpm and has 
                    better response than its predecessor. The new engine is also 
                    lighter and returns better environmental performance.<br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					<br>
					</font><b><font face="Verdana" size="2" color="#CC3300">
                    MAZDA 6 ULTRA<br>
                    </font></b>
					<font face="Verdana" size="1">
					The all-new Mazda6 Ultra was unveiled at the Motor Show in 
                    Oman. The Ultra’s MZI 3.7 V6 engine produces a rousing 275hp 
                    of power at 6,000rpm and 365Nm of torque at 4,250 rpm, while 
                    the MZR 2.5 four-cylinder powerplant generates 188hp at 
                    6,000rpm. A six-speed activematic transmission with Active 
                    Adaptive Shift (AAS) control is standard equipment for the 
                    V6 engine model. The car also offers a more extensive 
                    equipment list and its impressive features include dynamic 
                    stability control, an advanced smart keyless entry system, 
                    and independent rear cooling vents.<br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					&nbsp;<br>
					</font><b><font face="Verdana" size="2" color="#CC3300">
                    MITSUBISHI ECLIPSE</font></b><font face="Verdana" size="1"><br>
					<img border="0" src="auto2.jpg" align="right" width="250" height="167" hspace="8" vspace="6">The 2009 Eclipse GT will announce its presence strongly for 
                    2009, thanks to a new dual exhaust system that yields a more 
                    powerful sound while also boosting horsepower to 265 SAE 
                    NETT. Already equipped with a full array of proven safety 
                    technology, the Eclipse gains Active Stability Control (ASC) 
                    as standard equipment. One of the most prominent features of 
                    the Eclipse is the Rockford-Fosgate 650-watt audio system 
                    and is among the most powerful factory-installed mobile 
                    audio systems in the market today. It features a 6-disc, 
                    in-dash CD changer that can play MP3 CDs. The system plays 
                    through nine speakers, including a 10-inch subwoofer. The 
                    PUNCH control and Digital Signal Processor (DSP) give the 
                    user total control over the subwoofer’s “punch” level and 
                    provide custom music-genre equalization as well as sound 
                    field image settings. Powered by a 265-horsepower, 3.8-litre 
                    V-6 engine, the Eclipse GT plays in a serious league of 
                    performance cars. The engine employs the Mitsubishi 
                    Innovative Valve-timing Electronic Control (MIVEC) system to 
                    help optimize engine efficiency and power across a broad 
                    engine speed range. The GT’s 262 lb.-ft. of peak torque at 
                    4,500 rpm provides forceful off-the-line thrust. The Eclipse 
                    GT can accelerate from zero to 60 mph in about six seconds.
                    <br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					&nbsp;</font><b><font face="Verdana" size="2" color="#CC3300"><img border="0" src="auto3.jpg" align="left" hspace="8" vspace="6" width="209" height="175"></font></b><font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2" color="#CC3300">
                    AUDI 
                    RS6</font></b><font face="Verdana" size="1"><br>
                    The Audi RS 6 is the high-performance athlete of the A6 
                    family. With fascinating performance and breathtaking 
                    dynamics, it is in a league of its own – as the world’s most 
                    powerful series-produced sedan. The V10 engine with direct 
                    injection and twin turbo-chargers, eclipses its competitors 
                    with an output of 580hp and 650Nm (479.42 lb-ft) of torque. 
                    And at the same time, the new RS 6 offers all the comfort of 
                    the large executive model series from Audi. The Audi RS 6 
                    has performances figures worthy of the supercar segment. It 
                    sprints from a standstill to 100 km/h in 4.5 seconds and 
                    takes 14.5 seconds to reach 200 km/h. The electronic limiter 
                    ends the acceleration once a speed of 250 kmph has been 
                    reached.<br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					&nbsp;<br>
					</font><b><font face="Verdana" size="2" color="#CC3300">
                    <img border="0" src="auto4.jpg" align="right" hspace="8" vspace="6" width="200" height="187">VW 
                    PASSAT CC</font></b><font face="Verdana" size="1"><br>
                    The new Volkswagen Passat CC was revealed to the Oman 
                    marketplace at the motorshow, combining the rationality of a 
                    saloon and the emotional appeal of a sports coupé. The 
                    rationality of the Passat CC being a practical saloon is 
                    achieved by the four-door concept, the four fully sized 
                    seats and a significant level of interior variability. The 
                    coupé philosophy is reflected in a progressive, sporty coupe 
                    line as well as performance-orientated equipment. Volkswagen 
                    is offering the Passat CC exclusively with advanced direct 
                    injection engines worldwide. The Passat CC will come to the 
                    Middle East with two TSI engines. The first one is a 1.8 
                    litre engine with a power output of 160 hp (at 5,000 rpm). 
                    This turbocharged four-cylinder is exceptionally fuel 
                    efficient (average fuel consumption: 7.6 litres/100 km) and 
                    torque strong (maximal 250 Newton-metre at just 1,500 rpm).<br>
&nbsp;</font></p><hr color="#C0C0C0" width="90%" size="1">
                  <p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">
					<br>
					</font><b><font face="Verdana" size="2" color="#CC3300">
                    LEXUS LS600H</font></b><font face="Verdana" size="1"><br>
                    <img border="0" src="auto5.jpg" align="left" hspace="8" vspace="6" width="225" height="206">The all-new LS 600h L is powered by the ultra-quiet Lexus 
                    Hybrid Drive system, the automotive industry’s most advanced 
                    gas-electric hybrid system developed to date that features 
                    the world’s first full V8, all-wheel-drive (AWD) hybrid 
                    drivetrain. The system combines a new 5.0-litre V8 gasoline 
                    engine with two powerful electric motors and a newly 
                    designed large-capacity battery pack that is lighter and 
                    extremely quiet. The LS 600h L’s Lexus Hybrid Drive system 
                    provides power and performance on par with competitors 
                    normally aspirated 12-cylinder powerplants. The hybrid 
                    powertrain generates a peak combined output rating of 438 
                    horsepower and can effortlessly accelerate the LS 600h L 
                    from zero-to-60 mph in just 5.5 seconds. Mid-range 
                    acceleration is even more impressive. The system’s electric 
                    motor is capable of delivering maximum torque, on demand, 
                    and is most beneficial during passing manoeuvres. Finally, 
                    exhilarating torque is in abundance at higher speeds as the 
                    LS 600h L rapidly accelerates from 50-70 mph in a rapid 3.5 
                    seconds. The LS hybrid carries a Super Ultra Low Emission 
                    Vehicle (SULEV) rating.<br>
&nbsp;</font></p>
