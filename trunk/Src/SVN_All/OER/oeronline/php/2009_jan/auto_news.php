					<font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2" color="#CC3300">VW 
                    SCIROCCO</font></b><font face="Verdana" size="1"><br>
                    <img border="0" src="auto6.jpg" align="right" width="300" height="162" hspace="8" vspace="6">The new Scirocco is quite possibly one of the best looking 
                    hot hatches on the market today. It features a new design, 
                    high-end technologies such as DCC adaptive chassis control 
                    and the new 7-speed DSG dual clutch transmission. Volkswagen 
                    will offer the Scirocco with two TSI engines (160 hp and 200 
                    hp). Shortly thereafter, a TDI (common rail turbo-diesel, 
                    140 hp) and another TSI (122 hp) will be added to the engine 
                    lineup. Standard equipment on the Scirocco is the smoothly 
                    and precisely shifting 6-speed transmission. The DSG dual 
                    clutch transmission is offered as an option on the three 
                    engines starting at 140 hp. A 6-speed DSG is used with the 
                    TDI and the powerful 200 hp TSI. The 160 hp TSI distributes 
                    its power over an even broader range of gears with the 
                    7-speed DSG. Also on board are 17-inch alloy wheels, 
                    height-adjustable driver and front passenger seats, leather 
                    steering wheel, parking brake handle and shifting lever 
                    grip, sport seats, split folding rear seatbacks, power 
                    windows, air conditioning, ESP, braking assistant, six 
                    airbags, power steering and of course a sport chassis. On 
                    the top 200 PS model, standard equipment also includes 
                    chrome tailpipes, a “Titanium black” roof liner and a sound 
