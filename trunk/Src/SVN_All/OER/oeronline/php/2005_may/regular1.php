BankMuscat-NBO Merger</font><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="5" color="#FF0000">What went wrong?</font><font face="Verdana" size="1"><br>
<br>
<b>While ‘‘delays’’ might be the official reason for the shelving of the 
BankMuscat-NBO merger proposal, industry insiders feel that there is more to the 
episode than meets the eye. An OER analysis</b><br>
<br>
<img border="0" src="alkhal.jpg" align="left" width="200" height="248"></font><b><font face="Verdana" size="3">R</font></b><font face="Verdana" size="1">etracting 
from a merger proposal is not something new to company boards and shareholders 
in the Sultanate. Nevertheless, the scrapping of the mega-merger plan between 
BankMuscat and National Bank of Oman (NBO) after five months of serious 
negotiations and board meetings surprised everyone. Oman’s financial pundits are 
still at a loss to find a convincing reason for calling off of the merger, 
despite the fact that NBO management has made it clear that the only reason for 
it was the delay in reaching an agreement on certain issues. <br>
<br>
NBO, in a carefully worded statement to the Muscat Securities Market (MSM), 
said; “Keeping the best interest of the shareholders in mind, in view of the 
ongoing delays in reaching an agreement on the outstanding issues, it has been 
decided by the board not to pursue with the proposed merger”. The bank’s board 
took the decision by end-February, probably after evaluating a revised proposal 
from BankMuscat. Any delay in the proceedings could have created a feeling of 
uncertainty among both clients and employees of Oman’s second largest bank. <br>
<br>
<b>The inside story</b><br>
However, there is another side to the whole episode – this is nothing but a 
disagreement over evaluation of NBO’s net worth for merger consideration. The 
BankMuscat board decided to accept NBO’s net worth at RO97 million or the net 
worth of the bank audited by its statutory auditors and approved by the apex 
bank as at end-December 2004, whichever is lower. This revised figure is in 
keeping with the recommendations of the independent audit conducted by 
BankMuscat of the reasonableness of provisions in NBO’s books of accounts for 
last year. <br>
<br>
When the merger plan initiated last September, NBO’s net worth by end-December 
2004 was considered at RO100 million. It was also agreed that in case of a 
change in net worth, adjustments would be made through the revision of the value 
of bond, which was part of the swap ratio agreed by the banks. NBO earlier 
announced that its net worth was RO102 million, which was RO5 million more than 
BankMuscat’s estimate.<br>
<br>
<img border="0" src="suhail.jpg" align="right" width="200" height="248">BankMuscat management made it clear that NBO has expressed reluctance in 
proceeding with the merger plan and the merger talks with the latter have always 
been accorded the highest level of importance by the former. The board of 
BankMuscat had officially informed NBO (on February 28) of its decision to 
proceed with and conclude the merger talks at an understanding that NBO’s net 
worth by end-December should be estimated at RO97 million, in the absence of 
audited financial statements of the latter duly certified by the Central Bank of 
Oman (CBO), being provided to the bank. <br>
<br>
BankMuscat had made this offer recognising the importance of bringing the merger 
talks to a speedy conclusion so as to protect the interests of stakeholders of 
both banks. In fact, the merger plan was on a fast track till December last year 
as it had received approvals from both CBO and the Capital Market Authority 
(CMA). In the last couple of years, NBO was contemplating different options to 
resolve its problems, mainly on account of mounting non-performing assets (NPAs), 
and the merger plan was one of the options. Earlier, Suhail Bahwan Group pumped 
in more than RO50 million to take a 34.6 per cent stake in the bank in a move to 
enhance its capital base. <br>
<br>
However, slowly but steadily, the situation is changing at NBO. The bank has 
adopted a strategy to contain cost and concentrate on low cost deposits to raise 
net interest spread. There will be special focus on these measures during the 
current year. Like other banks, favourable external factors are also helping NBO 
to achieve a turnaround. The bank made a net profit of RO5.2 million last year 
as against a loss of RO51.7 million in the previous year. The better performance 
was mainly on account of a RO18.49 million recovery from provisions on credit 
losses in 2004 as against RO1.52 million for the previous year.<br>
<br>
Almost all banks in the Sultanate have shown marked improvement in net earnings 
due to a host of favourable factors last year. The interest spread was 
favourable for the financial institutions while stringent measures like 
corrective actions to bring down non-performing assets and cost cutting measures 
have helped them to enhance net earnings. There has been a special focus by 
banks on core cheap deposits and reducing expensive term deposits. Also, 
commercial banks have been gaining from low cost deposits and improvement on 
quality of assets. <br>
<br>
The additional capital from Suhail Bahwan group helped NBO to create a basis for 
improvement in net profitability. The capital adequacy ratio has increased 
substantially after the capital infusion. Earlier, NBO strengthened its capital 
position by means of a subordinated private placement to the tune of RO12.5 
million – which was subscribed by major public sector pension funds and private 
sector investors. <br>
<br>
The shares of both banks have moved up on the secondary market after the 
scrapping the merger proposal. BankMuscat stock forged ahead from RO7 on 
February 28 to RO8.85 on March 21, while NBO share firmed up from RO2.59 to 
RO3.24 during the period. There has been hectic activity in both counters in the 
last one month. Since NBO’s liquidity on the MSM is high, merger would have 
brought down the traded volume on the bourse substantially.<br>
<br>
<b>Failed M&amp;As: A history</b><br>
Rejecting merger proposals, even at the shareholders’ level, is not something 
new to the Sultanate’s financial system. Take, for instance, a merger proposal 
between Majan International Bank (MIB) and BankDhofar three years ago. 
Initially, the proposal was rejected by a section of shareholders on the ground 
that the agreed swap ratio was not attractive for MIB shareholders. Around 42 
per cent of shareholders rejected the proposal while 58 per cent voted in favour 
of the merger when it first came for approval at an extraordinary general 
meeting (EGM). According to Oman’s Company Law, a merger proposal can be vetoed 
if it is objected by more than 25 per cent of the shareholders. However, the 
merger proposal was cleared by the EGM later after changing the swap ratio in 
favour of MIB investors. <br>
<br>
In view of integration of GCC economies, it is desirable for small banks to 
merge with other relatively large ones with financial muscle. It helps the 
merged entity to avail of the benefit of economies of scale, as the merger will 
bring in synergies, besides adding value to the bank. Almost four years ago, CMA 
tried to reduce the number of brokerage firms through mergers. These mergers 
were aimed at helping brokerage firms to bring down their operational cost and 
thereby make their operations viable. <br>
<br>
After a slump on the MSM, majority of the brokerage firms were not getting 
enough business. The severe decline in trading volumes coupled with erosion in 
the value of investments in the post-1998 crash period had adversely affected 
the performance of brokerage firms. With an intention to reduce the number 
players on the bourse, the government offered soft loan to the extent of 50 per 
cent of the capital for a period of five years, exemption from profit tax for 
five years and additional business from a RO50-million government owned 
investment fund. The government also said that those firms that do not opt for 
merger would not qualify for any of these benefits. <br>
<br>
However, the CMA’s plans to create a handful of well-capitalised firms through 
mergers could not produce the desired results on account of some inherent 
shortcomings. Although few brokerage companies had announced their merger 
proposals to avail of the government incentives, a three-way merger (of 
erstwhile Al Mawarid Securities, Al Shalman Securities and the brokerage 
division of Oman &amp; Emirates Investment Holding) to form the Financial 
Corporation (Fincorp) was the only one to take a concrete shape. All others 
called off their plans at one stage or the other. <br>
<br>
The companies that called off their merger plan during the period were Gulf 
Investment Services and Financial Services, National Securities and Global 
Financial Investment and Oman Securities Portfolio Company and Al Shurooq 
Investment Services. Some of these proposals have even reached an advanced stage 
of conducting due diligence with the help of international consultants. The fear 
of losing family control and personal ego among board members were cited as 
major reasons for scrapping these proposals. <br>
<br>
<b>The silver lining</b><br>
However, the three-way merger, which took two years of negotiations and 
board-level meetings, was a tremendous success. The accumulated losses of some 
of these companies were wiped out before the merger and the merged entity has 
benefited from the synergies. Oman &amp; Emirates has 20 per cent holding, while Al 
Mawarid and Al Shalman have 38.5 per cent and 41.5 per cent stakes, respectively 
in Fincorp, which is now one of the leading brokerage houses on the MSM. <br>
<br>
Now, brokerage divisions of the Fincorp and Gulf Investment Services are 
planning to merge. The proposed merger is expected to form the largest brokerage 
firm on the MSM. As there are a lot of synergies among the firms, the merger 
would help to bring down operational cost and thereby improve efficiency. <br>
<br>
<b>Hope ahoy!</b><br>
Now everybody is watching the high-profile merger plan of two leading Oman firms 
– Renaissance Services and Topaz Energy &amp; Marine (TEAM). The merger of the two 
companies is expected to create a substantial entity in Oman’s services sector 
with an enhanced global footprint. The new entity with a net worth of around 
RO50 million will be able to look at growth opportunities of far greater 
magnitude than individual resources have hitherto permitted. <br>
<br>
The merger will benefit the shareholders as it will enhance the size and scale 
of operation and the risk-taking capacity will go up in view of high net worth. 
Moreover, it will open up new markets for the various businesses under the 
merged company and leverage of each other’s current relationships. Based on the 
internal evaluations, it is proposed that the swap ratio will be in the range of 
3.6 to 4.1 shares of TEAM for every share in Renaissance. This would give TEAM 
shareholders a stake of nearly 40 per cent in the merged company. If the merger 
concludes as per the above ratio, the capital base of the merged entity will be 
in the region of RO20 million.</font><font face="Marlett" style="font-size: 5pt">g</font></p>

