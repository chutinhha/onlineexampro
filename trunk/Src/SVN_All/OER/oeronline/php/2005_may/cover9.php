            <img border="0" src="behr.jpg" align="left" vspace="2" width="200" height="250"></b></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">“I</font><font face="Verdana" size="1">t’s 
            been a good year for OHI. We have been slightly over budget where 
            turnover is concerned, whereas we have been substantially over 
            budget where profitability is concerned,” exclaims Behram Divecha, 
            CEO, OHI. Launching into the details of OHI’s performance, he 
            reveals, “We were expecting a turnover of RO41 million, but we 
            recorded RO42 million. We made a profit of RO1.7 million while we 
            were expecting only RO1.2 million. The important factor here is that 
            the profit has been above performance, and that’s what matters.”<br>
            <br>
            Divecha attributes this growth to the company’s credo – 
            sustainability in whatever OHI does. He says, “We always planned and 
            worked towards sustained growth, shareholder value creation, 
            profitability and dividend. The company’s success also lies in its 
            proper management by the people concerned and professional managers 
            who head each division. We, in fact, give a free hand to our 
            managers who achieve the targets.”<br>
            <br>
            <b>Success galore</b><br>
            “In 2004, all the our five subsidiaries did well in terms of 
            profitability and growth. Each subsidiary contributed 
            (substantially) to the revenues of the company,” says Divecha. The 
            IT, Telecom and Construction subsidiaries had a successful year with 
            the bagging of important contracts for supply and installation of 
            new working infrastructures, servers and storage. Its clients 
            include Bar Al Jissah, Omantel, Ministry of defence, Muscat 
            Securities Market, and Oman LNG. <br>
            <br>
            One of the major contracts awarded this year to the company was for 
            the Sohar Power and Desalination Plant for all Civil and Building 
            Works by Doosan of South Korea. The plant is scheduled to produce 60 
            MW of power and 30 MIGD of water. In the marine sector, a new range 
            of 4 Stroke Yamaha Outboards and Jet Skis were introduced.<br>
            <br>
            DDB, the advertising wing of the OHI Group, performed well with the 
            firm bagging new businesses from Petroleum Development Oman (PDO), 
            Oman LNG, Mazda and Omantel. OHI Petroleum &amp; Energy Services and 
            Overseas Petroleum Equipment Co., the oilfield group of OHI, 
            registered another successful year in 2004. Some of the highlights 
            of the year for OHI are: <br>
            <br>
            • Five-year Frame Agreement to supply PDO with High Energy Pumps for 
            Water Injection and Oil Shipping duties<br>
            • Extension of the agreement for Harbison-Fischer Downhole pumps for 
            another two years<br>
            • Securing the framework contract for Harweel Cluster compressors 
            and the order for the third train at Oman LNG<br>
            • Orders for two Steam Turbine driven compressors for a fertiliser 
            project and two compressors for the Sohar Refinery, an axial flow 
            and a process gas screw compressor<br>
            OHI, along with its Russian principal NGKS, has also been awarded a 
            three-year contract to carry out intelligent digging of pipelines 
            using a Magnetic Flux Leakage technology. Another deal signed by OHI 
            is with PTTEP for mud engineering work. The company has also picked 
            up orders for centrifuges and shakers, which are used in oil 
            production work. Scientific Drilling, one of the principles of OHI, 
            has secured a new business for Downhole Survey work for PDO and 
            PTTEP. <br>
            <br>
            <b>Outlook 2005</b><br>
            The year 2005 promises to be yet another eventful one for OHI. The 
            company is in the process of forming a joint venture with a German 
            company to work together on various engineering projects. ‘‘We have 
            a lot of inherent combinations in various projects that are missing 
            a few key components. The German company will provide us with these 
            key components, which will help in the execution of the large 
            engineering projects we undertake. We will form a consortium to 
            handle power and energy projects,’’ says Divecha. <br>
            <br>
            At OHI, it isn’t just vertical growth that matters, but also 
            horizontal. And when it comes to horizontal growth, the name of the 
            game is value addition. ‘‘OHI is a major supplier of core equipment 
            to the petrochemical companies in the country. The company has 
            recently decided to expand its operations by venturing into other 
            domains. It is not just going to remain a supplier, but also move in 
            to the areas of supplying, stocking, commissioning, operating and 
            maintaining,’’ says Divecha. <br>
            <br>
            Similarly, in the telecom sector, OHI plans to extend its services. 
            Along with supply of the products to Omantel, Nawras and Oman 
            Mobile, OHI is also planning to run an operating service for them in 
            the near future. Internally, OHI is geared up to take on the new 
            challenges with the company providing staff training and also 
            upgrading their systems. “Improvements and changes are made on a 
            day-to-day basis in OHI where infrastructure is concerned. We 
            recently invested RO30,000 for upgrading our infrastructure,” 
            claims Divecha. <br>
