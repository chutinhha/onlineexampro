            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">
            It may have remained at the No. 9 rank in The OER 20 listings for 
            the second year running, but ever since Oman Holdings International 
            (OHI) went public in 1997, the company has grown manifold in all its 
            core areas of business. Without fail, OHI has improved its 
            performance levels every year and done better than the previous 
            year. The story continues this year too</font></b></p>
