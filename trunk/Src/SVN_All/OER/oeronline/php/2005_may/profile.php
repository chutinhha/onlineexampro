<br>
<b>Peter Schilter, GM – Oman, SWISS, talks to Vicky Kapur about his background, 
business and beliefs</b><br>
<br>
Tell OER about your career path...<br>
I graduated from the college for Public Transportation of Central Switzerland. I 
wanted to be an Air Traffic Controller because of my uncle – my mentor who 
introduced me into this job and I was fascinated by it. Ultimately, however, I 
chose to join Swissair instead. Within Swissair, I have had different 
assignments in Sales, Passenger Service, Cargo and Load Control divisions – so, 
I have been through several departments and received training in all these 
fields. <br>
For the last 20 years, I have been outside Switzerland, covering Swissair and 
Swiss International Airlines’ branch offices as Sales Manager and later as 
General Manager in many different countries on four different continents. I 
started off in Liberia in West Africa… from there, I moved to Cairo in Egypt, 
then to Los Angeles in the US. My next posting was in Northern Spain, from where 
I came to the Middle East – I became the GM of the Abu Dhabi branch, followed by 
an assignment in Tehran, as General Manager – Iran. That was my last posting 
before coming here – now I am happy to be in Muscat as General Manager – Oman.
<br>
<br>
How does Muscat compare with other Middle Eastern cities you have been posted 
in?<br>
Muscat is comparable with Abu Dhabi – both are kind of quiet cities with not a 
very large population whereas Tehran and Cairo are huge cities with 15 million 
inhabitants each with a lot of problems – congestion, air pollution, and crime, 
which we don’t know much about here. While I enjoy the absence of these factors 
here very much, for me, it isn’t as quiet because my job keeps me busy – Oman is 
a very challenging market. <br>
I have been here for a little over year now. Oman is a small market—less than 
2.5 million population—and there is limited potential for growth in passenger 
numbers. Yet, the country has considerable potential for in-bound tourism, and 
we see that happening now. We enjoy a small but loyal and very affluent 
clientele – a lot of them travel business and first class, and that’s why we 
manage to sustain and grow our business here. <br>
<br>
Please enlighten our readers about the management structure of your company.<br>
We are a branch office of Swiss International Airlines. We operate here under 
the sponsorship of United Travel, part of the Ajit Khimji Group of Companies. 
We’re subordinated to the Inter-continental Department of Swiss, which is 
subordinated to the Marketing &amp; Sales division. While I report to the head 
office in Zurich in Switzerland for some functions, my direct report is to the 
Vice-President of the Inter-continental Department. We have four departments in 
this branch office – Airport Operations, Sales, Cargo, and Direct Sales, headed 
by a departmental head each, who in turn reports to me. <br>
<br>
What does your role in the company entail?<br>
I am overall responsible for the sales and branch operations in Oman. My daily 
work entails mostly sales tasks because it is a very competitive environment, 
especially now that we have daily flights between Muscat and Zurich since 
November last year. That’s when the pace picked up quite a lot, and sales most 
definitely takes the lion’s share of my time. <br>
<br>
Does the number of people travelling from Muscat to Zurich justify a daily 
flight?<br>
Our route is clubbed together with Dubai, because just Oman does not warrant a 
daily flight operation at this time. Indeed, only about one-quarter of the 
Switzerland traffic is coming out of Oman while the rest is out of Dubai. But we 
also sell Dubai as a destination as we have a lot of empty seats from here. We 
have entered into a code-share agreement with Oman Air, and are accommodating 
Oman Air passengers between Muscat and Dubai on our flights. That adds about a 
100 passengers both ways.<br>
<br>
Briefly describe your management style.<br>
My management style is a mix of the delegation and hands-on approaches. While I 
love to delegate, the fact remains that we are a small team and I cannot always 
delegate everything I’d like to. We have regular meetings – weekly sales 
meetings, monthly management meetings, and I try to lead by example. <br>
I like to be accessible at all times, and want all my staff members to be 
comfortable talking to me at any time. I try to convince people of a particular 
decision rather than enforcing it. I look at generating consensus between the 
team because I am convinced that this approach works much better. I believe that 
people accomplish a task more efficiently if they have a part in the 
decision-making rather than when a decision is just forced on them. <br>
I like to set goals, targets and guidelines but, at the same time, leave room 
for the staff to be creative and make their own decisions and find their own 
ways to deal with a problem or pursue a project. I like to motivate the staff to 
perform at their best levels of efficiency. I push those who need to be pushed 
to perform better – there are, of course, people who do not need to be pushed as 
they are active on their own. <br>
However, from everybody, I demand good performance, high output, and a high 
level of efficiency because we operate under cost pressures and it is a must 
that all staff members are performing at their optimum level. I demand that my 
staff is service-oriented and that ‘the customer comes first’ – always. I also 
ensure that there is a good working atmosphere in the office and that the 
conditions for the staff are right, if work atmosphere is good, everybody works 
better.<br>
What are the challenges that your business faces in Oman?<br>
We had just three weekly flights before November last year, and now we have 
daily flights out of Muscat. That has more than doubled the capacity, and now 
the challenge is that we have a very steep increase in our sales target, which 
is going to be tough to fulfil. The pace is much higher, and the pressure on the 
staff is much greater than before. <br>
There is a certain overcapacity in the markets because a lot of airlines are 
throwing in more flights. The regional airlines are improving their network all 
the time. Both facts put pressure on the prices, and our average revenue per 
passenger is declining – which means that we have to achieve higher seat load 
factors all the time. So, it’s pretty tough competition even in a small market 
like Oman’s.<br>
<br>
How do you propose overcoming these challenges?<br>
Well, we have a great product – we have daily flights to Central Europe. We have 
excellent connections to most big European cities, and we are the fastest way to 
travel to Central Europe. Besides, we have an exceptional reputation – Swiss is 
known for good service. So, all these things will help us achieve our targets. 
Our competitors can buy nice airplanes and excellent in-flight entertainment 
systems – but to make a difference, you have to provide a quality product and 
good services to the customer. The human factor is the decisive factor, and I 
believe we score over there.<br>
<br>
Which management gurus do you favour and why?<br>
I have undergone intense training in TQL (Total Quality Leadership), which was 
developed by a company called ODI. Another area that I am interested in is 
service, and I favour Ron Kaufman, who was in Oman recently and is an advocate 
for good service. Quality and good service – I believe that these are the two 
most important factors that make a difference. <br>
<br>
What makes Swiss fly?<br>
At Swiss, we have a very modern fleet of Airbus 340s and 330s; we have daily 
flights to our hub-city, which is Zurich in Switzerland; Zurich has a brand new 
airport with a new terminal and a new main building; we have very good 
connections to most big European cities and to North America; and we have a very 
good in-flight and ground service; and the most beneficial factor is the caring 
attitude of our in-flight and ground staff.<br>
<br>
What according to you is the most important wing for your business?<br>
If you take a mix between the leisure and the business traveller, it would be an 
extensive network, a good product, competitive pricing, and an attractive 
frequent flyer program to attract repeat customers. A leisure traveller will 
probably choose the airline offering the best price whereas the business 
traveller will look for the fastest way to travel, and is willing to pay a 
little extra. <br>
<br>
What are your hobbies?<br>
I love to ski – so I go to Switzerland every year on a skiing vacation. Here in 
Oman, I like the beach, I like to play tennis, I like to travel – make 
excursions to the mountains and wadis, and I like to read, mostly newspapers, 
magazines and fiction novels..</font></p>

