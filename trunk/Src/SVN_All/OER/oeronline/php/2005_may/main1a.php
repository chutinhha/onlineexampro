<p align="justify" dir="ltr"><b><font style="font-size: 16pt" face="Verdana">
Swiss will survive</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><br>
<font face="Verdana" size="1"><b>Vicky Kapur met with Swiss International Air 
Lines’ Felix Rodel, Area Manager&nbsp; (Middle East, Pakistan &amp; Iran), in Dubai 
to understand the dynamics of the airline’s deal with Lufthansa. Excerpts from 
an exclusive interview:</b><br>
<br>
<b><img border="0" src="main1a.jpg" align="left" width="100" height="137">Swiss 
International Air Lines was in the midst of a turnaround. What are the reasons 
behind the Swiss board agreeing to a takeover by Lufthansa at this stage?</b><br>
This was done in a very small group around the CEO, and it came as a surprise to 
all of us. We clearly said we had to finish our restructuring, and the next 
priority would have been to tackle the alliance issue. Of course, now it’s done 
and announced. It’s an intention to take over, and it has to happen in stages – 
it isn’t something that’s a one-step issue. The major stakeholders have agreed 
to this – so, this is the way forward. <br>
<br>
However, at the same time, there are a number of hurdles on the way till we 
reach complete integration, which will take some time. The next three months 
would entail the first step – clearing the EU Competition Commission’s 
approvals. Then, the minority shareholders would be given an offer to purchase 
their shares. That will be the first step of acquiring about 19 per cent of the 
equity. Then at a later stage, in about 4 to 8 months, Lufthansa, together with 
Swiss will take the next step, which is acquiring 49 per cent of shares, and 
only after about one-and-a-half years do we believe that there will be a 100 per 
cent takeover of shares. <br>
<br>
This has to do with the fact that in this industry, you need to secure the 
ownership question, the traffic rights issues that are an important asset to an 
airline – perhaps similar to what is happening in the Air France-KLM merger. All 
this requires a certain amount of time to happen. <br>
<br>
What we see as an immediate step is basically network adjustments for the winter 
timetable, which starts in October. We will try to get the frequent flyer 
benefits aligned earlier—if possible, during summer—so that Swiss Travelcard 
members get the benefits of accruing and redeeming miles on the Lufthansa 
network and vice-versa. <br>
<br>
<b>What do you have to say about the timing of the takeover?</b><br>
That we were able to better our results dramatically over two years is a fact 
that was recognised by the management of Lufthansa and, therefore, the value of 
the partnership with Swiss became more interesting. We had had discussions with 
Lufthansa in the past over Swiss’ performance, and it was apparent that we would 
take these steps and improve our performance over the said period of time. So, 
this is a positive sign if you like, that we were on the right track. <br>
<br>
Another thing that needs to be taken account of is that we do need to grow – and 
that was a message that you heard in Switzerland too. After the start of 2002, 
we had to grow through a series of steps of resizing the company, but at the 
same time, we also knew that where business was profitable, we had to be able to 
increase our offer. <br>
<br>
Now imagine the situation you are in – you are starting to break even and there 
is a perspective which opens… In this industry, you need to plan about 12 months 
in advance as aircraft purchases and network adjustments etc. can’t be done 
overnight. So, this is what we’ve seen – we’ve seen the future – we need to grow 
into certain markets and regions; at the same time, our major stakeholders give 
out a signal that they are not interested in investing more into the company.
<br>
<br>
We always maintained that we would get profitable towards the end of this year 
or perhaps beginning of next year. We also see that in 2006, we should start to 
improve on certain network expansions – and the only way to do this is to find 
someone who is willing to believe that this is actually going to happen and who 
is also interested in putting his money behind it. Obviously, this is now 
happening with the partner that we’ve found – Lufthansa is willing to invest. 
They are also willing to make it possible for us to operate two more long-haul 
aircraft in 2006. <br>
<br>
There will be more changes happening later – and this is what we need. So, it 
makes sense that this step was taken; otherwise, we would have been there, but 
we wouldn’t have had the strength to actually grow. <br>
<br>
<b>Could you give OER the details of the deal?</b><br>
The goal of this deal is full integration of Swiss into the Lufthansa group. Due 
to competition laws and anti-trust regulations, this has to be done in phases. 
The shares of Swiss will now be held in a new company called Air Trust. 
Lufthansa, as a first step, will acquire 11 per cent of this new company. Once 
anti-trust approval is gained, the stake would be raised to 49 per cent.<br>
<br>
We will start negotiations to secure all our (140-odd) bilateral air traffic 
rights between Switzerland and other countries with a third-party ownership 
clause. Once that is done, Lufthansa will buy the remaining 51 per cent shares 
of Air Trust and assume full ownership. The minority shareholders will get an 
offer by Lufthansa, which will be an average of the past couple of weeks’ 
performance of the Swiss share. Based on current information, these 15 per cent 
of total Swiss shares will have a value of something between Euro45 million and 
Euro50 million. This transaction will happen sometime in May. <br>
Major shareholders get an out-performance option in exchange for their shares, 
and this option will be tied to the performance of the Lufthansa share in 
comparison to a basket of other European airline share performances. Based on 
the positive performance of the Lufthansa share, the maximum payout to the 
majority shareholder could amount to approximately CHF400 million.<br>
<br>
There is an initial amount in the area of CHF60 million. There is an agreement 
with the majority stakeholders—the government, some of the large companies—that 
they get a sort of an IOU, which would then be coming into play in about 
one-and-a-half to two years’ time. The performance of the Lufthansa shares in 
comparison to an average of European shares would then trigger a payback in 
2007. Then, if you take the best possible scenario, the purchasing price could 
be in the area of CHF350-400 million. <br>
<br>
<b>How do you think Lufthansa will benefit from this takeover?</b><br>
In my opinion, from the Lufthansa group’s point of view, there are several 
positives in this acquisition. On the one hand, Lufthansa is able to grow into a 
very lucrative market—Switzerland—which is a high-worth market with a high 
percentage of business travel. They have tried to increase their market share 
over the last couple of years in Switzerland, and I think the acquisition opens 
up a much larger scope now.<br>
<br>
Then, another positive that I can see for Lufthansa is that hub Zürich is a 
modern and a well-functioning hub (see box), which can work very well in 
combination with two other main hubs that Lufthansa is operating today, which 
are Frankfurt and Munich. This triangle, so to speak, gives a very strong 
position to the two companies operating in Europe, and it will help to 
strengthen the feeding and the de-feeding networks that traditionally larger 
airports rely on. <br>
<br>
So, there will be a possibility to channel traffic from the Lufthansa network 
through the hub of Zürich into the Swiss network and also vice-versa to certain 
destinations. We can make best use of complimentary network opportunities. The 
airport will become more interesting for the customer who today wants to have 
the highest possible choice with the most flexibility and if you then are able 
to offer a very dense network with a large number of destinations that can be 
reached by using three major transport points, you are able to attract customers 
onto your network, and they’ll like to stay on your network without utilising 
other carriers. <br>
<br>
<b>Why Lufthansa?</b><br>
If you look back in history, there have been discussions on possible alliance 
partners, and we were once very close in getting a deal done with the Oneworld 
alliance. Those negotiations were stalled at the last minute because the 
conditions set by the Oneworld partners were just not acceptable at that point. 
An alliance always has to be a win-win situation, and if you feel that you are 
giving more than you get, then I believe that is what stalls that approach. 
Nevertheless, that alliance partnership was also in certain points 
complementary, and it would have worked too. <br>
<br>
In the case of the Swiss-Lufthansa tie-up, what also needs to be said is there 
is similarity in the approach of the two carriers. We share similar philosophy, 
want to generate high quality service through an innovative approach. We even 
speak the same language—not in the whole of Switzerland of course, but at least 
a majority of the Swiss market. So, there is a cultural affinity, if you like, 
and this should make it easy to achieve common goals.<br>
<br>
<b>Will the Swiss brand survive this takeover?</b><br>
I think what the two CEOs have expressed is an understanding that the value of 
this acquisition would be best utilised and exercised if brand Swiss continued 
to exist in its current form – it has a strong value, it has a strong awareness, 
and it would be perhaps a wrong approach if this was now levelled out or 
integrated into the Lufthansa brand, which has an equally strong standing but 
still is a different product as such and has a slightly different image attached 
to it. <br>
<br>
So, the brand as such will remain. It is also an intention that the Swiss Air 
Lines will keep its appearance, its logo, its own fleet, with its own crew, own 
management with its heart in Zürich, and I think the important thing is that we 
should also function with out own profit and loss responsibility. So, there will 
be two brands or fronts to the customers offering joint benefits which make it 
easy to use, and in the back we will try to combine and benefit from things like 
economy of scale or joint use of resources, joint use of training, of 
procurement etc. <br>
<br>
The intention is for the customer to recognise that there is a brand Swiss, 
which has its brand image and its brand language and its expectations and its 
service history while at the same time recognising that there is brand 
Lufthansa, which has its own type of personality and these two would coexist 
next to each other, giving (the customer) the opportunity to use the two side by 
side. <br>
<br>
<b>So Swiss will cease to be Switzerland’s national carrier…</b><br>
Well, in a sense, it will be the dominant carrier – let’s put it that way. 
National carrier by ownership? No. National carrier by affinity? Yes. But, quite 
frankly, the concept of national carriers in Europe, over the next couple of 
years, will change. This is something that is being discussed and to a certain 
extent also already introduced if you look at the KLM-Air France situation. <br>
<br>
<img border="0" src="maina1.jpg" align="right" width="150" height="110">We 
believe that there will be a couple of major European carriers or groupings of 
carriers that would emerge and a number of niche players that would have a role 
to play next to perhaps two to maximum three low-cost carriers. This would, over 
time, call for a different form of ownership. <br>
<br>
Today, the EU has welcomed the formation of such joint jointly owned airline 
companies, and the concept of each nation having its own flag carrier will be 
come less and less important. I think it will be more the brand, the quality, 
the network, the type of quality you get that will have an influence; and less 
the flag that actually binds. It will take time, but I could well imagine that, 
in future, the brand name would be more important than the flag.<br>
<br>
<b>Will Swiss continue to focus on destination Switzerland?</b><br>
For hub Zürich to play its role within this new group<img border="0" src="main1ab.jpg" align="right" hspace="3" width="196" height="110">, 
we will focus on the market Switzerland, and we will offer the Swiss market main 
intercontinental destinations as we do today and will open up a lot of new 
opportunities within Europe with a denser network with code-shares that we can 
offer on each other’s operated flights. <br>
<br>
From a Swiss customer’s point of view, he will continue to have the current 
offer on long-haul and medium-haul flights that are Swiss operated, he will 
continue to have a number of European flights that are Swiss operated, and he 
will increasingly have more and more options to use code-shared flights both 
within Europe and to certain long-haul destinations that we do not fly yet. 
This, of course, works both ways – there are a number of destinations or areas 
where we are strong—say, in Africa—where customers from the Lufthansa network 
would profit. <br>
<br>
<b>What about the network overlap with Lufthansa?</b><br>
Duplication mainly would be in the neighbourhood traffic, where you see Swiss 
and Lufthansa serving destinations between Switzerland and Germany, and that’s 
what I mentioned earlier that we are going to see network adjustments for 
winters. <br>
<br>
We will make sure that we offer the best possible connectivity between 
Switzerland and German destinations, either by coded flights or own operations, 
and this set would then give us the opportunity to reduce the amount of 
operations we have to produce on our own to Germany, and we can then deploy that 
capacity elsewhere, where we would be more interested to protect our market 
share and to grow. <br>
<br>
We can at least attempt now to come back with certain strengths that perhaps 
before weren’t as apparent if you have to stretch a limited amount of aircraft, 
which does not allow you to make a big, strong impression on particular routes.
<br>
<br>
<b>Will there be a reduction in the number of Swiss Air Lines employees?</b><br>
Today, it’s a bit too early to really go into details of this nature. We believe 
that, in future, they will be following the network development – areas where we 
can combine forces, or areas where we can take over on behalf of a partner. So, 
I believe that this new situation would actually give us more opportunities to 
jointly exercise our strengths, but since this is something that will follow the 
development of the network, it’s too early really to say either this or that is 
going to happen. <br>
<br>
Certainly, it is possible that, if for instance, two similar types of 
organisations are now managed jointly, the number of total staff is perhaps not 
going to be the same. But we will see how that will evolve and this is the 
development as I said which starts in about four to five months and it will take 
another one-and-a-half years or so.<br>
<br>
What will be the impact of the takeover on Swiss’ Middle Eastern operations?<br>
We have to wait for another four to five months to get the necessary approvals 
to actually start implementing the joint synergies, but in the Middle East—same 
as with networks in other regions—I believe we can already offer our customers 
the benefit of access to the larger Lufthansa network. <br>
<br>
I believe we have strengths in certain markets that will make our offer 
beneficial to Lufthansa customers as well. Look at Muscat, for instance, where 
Swiss operates on a daily basis. We see certain amount of destinations that are 
interesting for us that could be code-shared in future. I think the combined 
effect should be that we are able to jointly offer a wider network and increase 
the attractiveness of our offers. <br>
<br>
We are already running respectable seat loads on most of our routes – so, we 
will definitely try to improve further on efficiency. I don’t expect that we 
will immediately see growth in added frequencies from our side. We’ll first try 
to exercise the benefits of working together, and then take it from there. It 
will definitely be our aim that one and one is more than two. <br>
<b><br>
What are the future projections for Swiss now?</b><br>
We will be occupied now with this occupation in the next couple of months. We 
are also looking at growth, which would be a 2006 priority. With the intention 
of getting two more long-haul aircraft, we would definitely look at opening 
additional destinations – it’s perhaps too early to say which ones. <br>
<br>
We would also look at strengthening the current network – we have certain 
seasonal imbalances that we were forced to do because the 18 aircraft covered so 
many rotations only, and with additional capacities, we can then come back to 
year-round, stable, daily products. It is more economical to run a daily or a 
regular schedule throughout the year.<br>
