<b>Vicky Kapur gets face2face with HE Stuart Laing, Ambassador, British Embassy 
Muscat, to learn about the Oman visit of the Lord Mayor of the City of London</b><br>
<br>
<img border="0" src="main2.jpg" align="left" width="120" height="149"></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">he 
City of London is at the heart of the world’s financial markets. It is a unique 
concentration of international expertise and capital, with a supportive legal 
and regulatory system, an advanced communications and information technology 
infrastructure and an unrivalled concentration of professional services. The 
City’s governing authority, the Corporation of London, is committed to serving 
the needs of international business and maintaining the environment in which 
organisations and companies from all over the world can play their part in 
financing global trade and development.<br>
<br>
As head of the Corporation of London, the Lord Mayor of the City of London 
presides over its governing bodies – the Court of Aldermen and the Court of 
Common Council. He is Chief Magistrate of the City of London, Admiral of the 
Port of London, Chancellor of City University and President or Patron of many 
other civic and charitable organisations. He also has a much wider business role 
– working for Britain as a whole, supporting and promoting the world’s leading 
international financial centre, the City of London, both at home and abroad. His 
position as a trusted spokesman for the business community is enhanced by the 
entirely apolitical nature of his office.<br>
<br>
A key responsibility for the Lord Mayor is to promote the UK-based financial 
services industry and related businesses abroad. The City of London is the heart 
of this industry, but it also includes the UK retail financial services centres 
in cities such as Edinburgh, Glasgow, Manchester, Leeds, Birmingham and Bristol. 
During his year of office, the Lord Mayor spends between 50 and 80 days overseas 
promoting this industry and the City of London as a place of excellence in which 
to do business. <br>
<br>
Accompanied by a senior business delegation, he meets and makes representations 
to government ministers and other authorities. Important contacts are made at 
the highest levels – links which are made increasingly valuable by the growing 
international participation in the City and in British manufacturing industry. 
As a part of this role, the Lord Mayor of the City of London will be in Oman on 
a 5-day visit to promote trade and commerce with Omani business. OER meets with 
HE Stuart Laing, Ambassador, British Embassy Muscat, to learn more about the 
Lord Mayor’s visit.<br>
<br>
<b>Tell us about the office of the Lord Mayor…</b><br>
The Lord Mayor of the City of London is the representative of the financial and 
other services that the City of London is famous for. By the City of London, I 
mean what is sometimes called the Square Mile, which is the very small area in 
the centre of London—the oldest part of London—where the banks and the main law, 
accountancy, and consultancy firms have their headquarters. <br>
<br>
The City of London should not be confused with greater London, which is the wide 
area of London – and it’s important to make a distinction between Lord Mayor of 
the City of London and the Mayor of London. Ken Livingston is the Mayor of 
London, and is responsible for the utilities and services in greater London. The 
Lord Mayor is a very ancient office—going back several hundred years—and he is 
the ambassador for London as a financial centre. <br>
<br>
The Lord Mayor himself is a man called Michael Berry Savory, who is a 
stockbroker, and takes a year off from his firm in order to be the Lord Mayor of 
the City of London. It’s an annual appointment, and the Guilds, which are very 
old trade associations of the city of London, elect the Lord Mayor. The Guilds, 
through the centuries, have modernised and brought themselves up-to-date to 
maintain London as a leading financial centre. <br>
<br>
<b>Why is the Lord Mayor coming to Oman?</b><br>
We usually try to arrange for the Lord Mayor to visit Oman every three or four 
years. The last visit of the Lord Mayor was in 2001, and his aim is to present 
London as a financial centre and cement the relations that already exist between 
the Omani business and financial community and their British opposite numbers.
<br>
<br>
<b>What does his itinerary include and how long will he be here?</b><br>
The Lord Mayor is here for three working days, but also for most of the weekend 
before. We hope that the Lord Mayor will have the honour of an audience of His 
Majesty the Sultan, but we don’t have confirmation of that yet. <br>
<br>
The Lord Mayor’s formal host is HE Hamood Sangour Al Zadjali, the Executive 
President of Central Bank of Oman (CBO). He will also have meetings with HE 
Ahmed bin Abdulnabi Macki, the Minister of National Economy, and HE Maqbool Bin 
Ali Sultan, the Minister of Commerce &amp; Industry. He will also meet HE Yahya bin 
Said Al Jabri, the Executive President of the Capital Market Authority (CMA).<br>
<br>
We have made his visit coincide with a meeting of the Oman-Britain Friendship 
Association, which is really a joint business council. HH Sayyid Haitham is the 
patron of OBFA, and has very kindly agreed to host a lunch for the OBFA members 
and the Lord Mayor. That will bring together the Lord Mayor, who comes with a 
delegation of about half-a-dozen bankers and financiers, with the OBFA members.
<br>
<br>
<b>How will the Lord Mayor’s visit boost business ties among Omani and UK 
businessmen?</b><br>
We hope to enhance the contacts between the City of London and the CMA and the 
stock exchange (Muscat Securities Market). We might possibly arrange a series of 
visits or business exchanges, and I hope that the financiers and bankers 
accompanying the Lord Mayor will have opportunities for networking with their 
Omani contacts, and I hope that some business will develop from that – business 
to the advantage of the British and the Omani people. <br>
<br>
There is another happy coincidence, which is that the Lord Mayor’s visit 
coincides with a conference on May 1 being organised by the CMA on corporate 
governance. The Lord Mayor himself is very keen on this issue and will address 
the conference. But the main speaker of the conference is another British 
businessman called Sir Robert Smith, who is chairman of the Weir Group.<br>
<br>
The Lord Mayor will meet businessmen as well – he’s going to make a public 
speech to an invited audience at the Grand Hyatt hotel on April 30. I hope that 
a good number of business people are present there so that they can hear him 
speak. <br>
<br>
<b>Will his wife accompany the Lord Mayor?</b><br>
His wife, Mrs Savory, who carries the title of the Lady Mayoress, will indeed 
accompany the Lord Mayor, and we have arranged a programme for her which is 
partly independent and partly joins on with the Lord Mayor. She has some 
charitable interests, and we hope to arrange some meetings with ladies for 
meeting that particular objective. <br>
<br>
We also hope that she will pay two courtesy calls on two lady ministers – the 
Minister of Higher Education, HE Dr Rawya bint Saud bin Ahmed al Busaidiyah, and 
the Minister of Social Development, Dr Sharifa bint Khalfan bin Nasser al 
Yahya’ee. An important event will be a seminar for Women in Business, at my 
residence, on the morning of Sunday, May 1. The Lady Mayoress will address the 
seminar. <br>
<br>
<b>Could you please provide an overview of the British-Omani trade thus far?</b><br>
It has been a very successful relationship – exports from the UK to Oman have 
gone up 30 per cent in 2004 compared with 2003. Visible exports from the UK are 
mostly in the engineering sector, especially in oil engineering services and 
products. We have significant invisible exports as well, not least because we 
have a large British community here – about 6,000 people. I know this is not 
large when you compare it with some of the other expatriate populations here, 
but for us, it’s quite a lot. <br>
<br>
And these British people are spread about a whole range of activities – some 
professionals, e.g., lawyers, accountants and business consultants; some 
professionals of a different kind, e.g., in medicine and in teaching; and a 
large number of managers of business and other workers. So, all these people 
help in selling British services to Oman, and I am glad to say that British 
services of a fairly wide range are greatly appreciated in the Omani market. <br>
<br>
There is an imbalance in trade which is to say that we, the British, don’t buy 
so much from Oman as Oman buys from us. But I don’t think that this should worry 
either side because Oman’s principal exports are oil and gas, which are sold 
especially to markets in the Far East, which are very satisfactory and give the 
country a good income. <br>
<br>
I am a little disappointed that we have not succeeded in attracting more British 
investment into Oman. It’s difficult to know exactly what is the reason for 
this. Sometimes, it’s been sheer bad luck – that’s to say that a British company 
has made a bid or a tender for an investment project and has not succeeded in 
winning it. So, no one can be blamed for that. <br>
<br>
By and large, British companies invest very large amounts abroad, and we have a 
very good record of investing abroad. We are looking at ways of how we can get 
more of this British overseas investment to come to Oman – because the message 
that I am sending to British business is that this is a stable environment and a 
good one in which to invest.<br>
<br>
<b>How many tourists come from the UK to Oman?</b><br>
I haven’t seen the latest figures, but Britain usually comes second or third on 
the list of foreign tourists. I think the Germans usually head the list, with 
the French, the Italians and us not far behind. There’s good and bad news here 
for Oman – the good news is that the British tourists tend to come for 
comparatively longer visits. The not-such good news is that they are very often 
what the trade calls VFR (visiting friends &amp; relations). <br>
<br>
Because of this large expatriate community resident here, people come to see 
their friends and relatives. So, British tourists stay less in hotels than, for 
example, German tourists. However, when you come visiting friends and relations, 
you use the restaurants, you visit the hotels, you certainly visit Oman’s 
magnificent tourist sites…people hire cars. So, I think the British tourists do 
actually contribute to the economy even when they are visiting friends and 
relations.<br>
<br>
<b>What about the traffic the other way round?</b><br>
From the visas issued to Omani nationals, I can say that our visa traffic is 
steadily rising. One quite interesting fact is that although you hear a lot more 
about Omani students going to universities in the East—Malaysia, Australia, New 
Zealand etc.—our student visas have remained steady or little bit on the 
increase. I am delighted to say that more than 30 British universities and 
institutes were present at the GHEDEX 2005. The British institutes of higher 
learning certainly see great potential in attracting Omanis to study in the UK.<br>
<br>
<b>Which is the largest company from the UK in Oman?</b><br>
I suppose it must be Shell, as part of the PDO joint venture. Shell had some 
difficult corporate issues to work out last year, but I think is now working 
very effectively. I am particularly attracted by what they do in corporate 
social responsibility – e.g., their Intilaaqah programme, which helps educate 
young Omanis and bring them forward into running small businesses. It seems to 
me that the extension of the Small and Medium Enterprise in Oman has to be the 
way forward for providing more jobs for young Omanis, which is so important for 
Omani society and the economy in coming years. <br>
<br>
We have other significant companies as well who are either already here or are 
about to come here. I should like to highlight the company SERCO, who in joint 
venture with the Suhail Bahwan Group, are the preferred bidder for the Military 
Technological College, and we hope that that contract will be signed very soon. 
This is a very interesting and ambitious project, being driven by the Omani 
Ministry of Defence, which will be responsible for educating a large number of 
Armed forces personnel, and we hope to spread to have civilian students within a 
few years. <br>
