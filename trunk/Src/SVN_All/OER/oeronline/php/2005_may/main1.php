<p align="justify" dir="ltr"><b><font face="Verdana" size="2">Swiss 
Air–Lufthansa tie-up:
</font><font face="Verdana" style="font-size: 11pt"><br>
</font><font face="Verdana" style="font-size: 16pt">The inside story</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><br>
<font face="Verdana" size="1"><b>Vicky Kapur provides a bird’s-eye view of the 
restructuring underway in the ailing Swiss International Air Lines in light of 
the recent takeover announcement by Lufthansa. An exhaustive report</b><br>
<br>
<img border="0" src="main1.jpg" align="right" width="150" height="101">If you close your eyes and think of Switzerland, what comes to mind are 
chocolates, cuckoo clocks, the Alps, Swiss knives – and Swissair. In a 
near-collapse a few years ago that stunned the industry and the travel world, 
the airline, a byword for quality in the trade, and re-born as Swiss 
International Air Lines, or Swiss, has once again been reincarnated. When German 
airline Lufthansa announced the taking over of the Swiss International Air Lines 
in late March, the timing came as a surprise to many in the industry, including 
high-ranking officials within the Swiss carrier. <br>
<br>
That the Swiss national carrier was ailing and in dire need of cash infusion was 
no secret – in fact, merger talks with Lufthansa had been going on for a while, 
after the much-touted deal with Oneworld Alliance had fallen through earlier. 
What was surprising, though, was the fact that, having announced another round 
of restructuring in January this year, the airline seemed on course to achieving 
break-even results, and would have been in a much better position to negotiate a 
deal when its books weren’t in the red.<br>
<br>
In fact, exactly a fortnight before the merger announcements, OER was in Zürich, 
and was made presentations by senior Swiss representatives, including Oliver 
Evans, chief sales &amp; marketing officer, and Jürg Dinner, vice-president, 
Corporate Communications, on the restructuring of the airline and the turnaround 
that was underway. <br>
<br>
Sure enough, the airline’s 2004 financial results made available to OER days 
before they were made public did signify a major improvement (see table). Swiss 
cut its consolidated net loss to 140 million Swiss francs (CHF) (90 million 
euros; $118 million; RO45 million) in 2004, compared to net losses of CHF687 
million ($579 million; RO223 million) in 2003. Jürg Dinner, in fact, made no 
bones about the fact that the airline still had some way to go before achieving 
profitability, and said that there was a ‘‘rocky road ahead’’. <br>
<br>
As per the latest round of resizing, the airline was to give up 13 aircraft and 
some 1,000 employees were to lose their jobs in the subsequent 18 months 
(January 2005 onwards), in order to resolve the financial problems. While the 
airline is expected to go through with this planned downsizing, the takeover is 
definitely good news for the Swiss national carrier in which major shareholders 
were clearly not interested in investing any more money. <br>
<br>
Its bigger and healthier German rival clinched the agreement after major Swiss 
shareholders, led by the Swiss government with its 20.4 per cent stake, approved 
the deal worth up to 310 million euros ($409 million; RO157 million). Lufthansa 
and Swiss said in a joint statement that their boards had endorsed the 
agreement, which was later signed by Swiss CEO Christoph Franz and his Lufthansa 
counterpart Wolfgang Mayrhuber in Zürich. <br>
<br>
Other key Swiss shareholders, including Swiss banks UBS and Credit Suisse, 
regional authorities, and Swiss blue-chip firms such as Nestlé, Novartis and 
Roché, cast aside national pride and lifted their traditional resistance to 
Lufthansa amid Swiss’ clouded financial horizon. ‘‘As a member of the Lufthansa 
group, Swiss will be better placed to assume its role in linking Switzerland to 
the rest of the world over the long term,’’ Franz was quoted to have said during 
the takeover announcements.<br>
<br>
Mayrhuber, on the other hand, said Lufthansa’s customers would benefit from more 
destinations, more connections, and improved service. ‘‘The merger of our two 
companies is not only positive for Switzerland and Germany but also for our 
partners in Star Alliance. It reinforces European air transport,’’ he 
maintained. Federal and local authorities along with Swiss businesses owned 86 
per cent of the airline, having invested more than CHF2 billion (RO648 million) 
under a national rescue package put together after the former flag carrier 
Swissair went bankrupt in 2001. <br>
<br>
Swiss has been plagued by heavy losses form the outset, but had managed to 
narrow them over the past year amid a massive restructuring drive that slashed 
one-third of the company’s staff and as much of its fleet. Former Lufthansa 
executive Franz, who took over as chief executive at Swiss in June 2004, 
announced more cuts this year as Swiss grappled with rising oil prices and 
struggled against budget competition in Europe. <br>
<br>
He reiterated that those cuts must go through to ensure Zürich’s value as a hub 
to the new group. Franz warned earlier, during the public announcement of its 
2004 annual results, that operating results at Swiss were still unsatisfactory, 
urging staff unions to agree to a further 1,000 job cuts over the next 18 months 
and renegotiate collective contract agreements by the end of 2005 to cut costs.
<br>
<br>
Executives at Swiss had staked the fledgling company’s long-term survival on 
joining a major industry alliance as early as 2002. But a deal with British 
Airways on joining the Oneworld Alliance fell through a year ago, prompting the 
revival of simmering behind-the-scenes talks with its huge German rival. <br>
<br>
Analysts have predicted that the deal should speed up consolidation in the 
European airline industry as more carriers join forces to compete against new 
low-cost competition. In the past, Lufthansa tended to steer clear of big 
acquisitions, opting instead for code-sharing agreements under the umbrella of 
its Star Alliance grouping. <br>
<br>
Interestingly, Swiss will still be an autonomous unit with the name, logo and 
its well-known identity retained. According to the jointly developed business 
model, Swiss is to remain an independent airline with its management and seat in 
Switzerland, its own fleet and crew, but operating within the Lufthansa system. 
Swiss will retain its strong brand image, develop its strengths, and expand its 
customer base in the local market, and overall, observers maintain that 
long-term prospects could be opening up for Swiss International and its 
employees. <br>
<br>
Currently, the Swiss-Lufthansa set-up seems to be similar to that between KLM 
and Air France. Mayrhuber emphasised the benefits of the integration for both 
airlines in a simple statement: “Our company and Swiss, two world renowned 
airlines with a strong sense of quality and service, are joining forces, with 
clear benefits for our customers.” <br>
<br>
There is also hope that Lufthansa’s network will help feed passengers into 
Swiss’ hub at Zürich airport and boost profitability. The Lufthansa takeover is 
the second biggest of its kind in European history, after the acquisition by Air 
