            Maha Petroleum Products Marketing Company (Al Maha) secured the No. 
            3 position in The OER 20 rankings within less than a year of 
            becoming a public limited company. The company achieved this despite 
            the highly competitive scenario that existed. Al Maha was 
            transformed from a limited liability company to a joint stock firm 
            subsequent to a decision taken by the Ministry of Oil &amp; Gas and the 
            Central Bank of Oman to divest their 65 per cent stake in the 
            company. Of this, 60 per cent was offered to the public while 5 per 
            cent went to ABS Lubricants.<br>
            <br>
            The competition faced by Al Maha was stiff, but that did not deter 
            the company from achieving increased sales that went up by 3 per 
            cent compared to 2003 and a net profits that was up by 23 per cent. 
            The turnover recorded in 2003 was RO74.9 million whereas in 2004, 
            the figure touched RO77.5 million. When Al Maha registered RO2.1 
            million as net profit in 2003, the figure reached RO2.6 million in 
            2004, an increase of RO484,795 that was attained as a result of 
            better gross margin and increase of sales. The gross profit achieved 
            by Al Maha stood at RO10.2 million in 2004 and has increased by 
            RO1.61 million from the RO8.59 million that the company managed in 
            2003.<br>
            <br>
            To top it, the former board of directors passed a resolution to 
            reduce the share capital of the company from RO8.5 million to RO6 
            million (29 per cent). This led to an increase in earnings per share 
            by 58 per cent. It went up from 247 baisas in 2003 to 390 baisas in 
            2004. As for the amount RO2.5 million that was derived as a result 
            of the reduction in share capital, it was repaid to the Ministry of 
            Oil &amp; Gas, the Central Bank of Oman and ABS Lubricants in proportion 
            to the shares held by them during that time.<br>
            <br>
            <b>Business management</b><br>
            Alhough there was (and is) stiff competition in finding feasible 
            sites for setting up filling stations, Al Maha was able to open 
            eight filling stations that were commissioned in 2004. This brought 
            up the total number of filling stations to 110 at the time of 
            closing the books in 2004. With the introduction of new filling 
            stations, there was an increase of 23 per cent in retail sales in 
            the last financial year compared to 2003. <br>
            <br>
            Al Maha plans to introduce new services in filling stations such as 
            providing car wash facility for the customers although competition 
            is no less. On the contrary, if attractively priced and innovatively 
            marketed, this could improve the sales in filling stations. Al 
            Maha’s handy fuel cards have also been business boosters, 
            contributing to increase in sales. The company's convenience 
            outlets, Souq, have been steady in their sales growth and have 
            contributed to the increase in retail sales.<br>
            <br>
            With regard to commercial sales, Al Maha achieved a growth of 7 per 
            cent in 2004 compared to 2003. New business and non-sustainable 
            market conditions were the main reasons for this. The company plans 
            to sustain its market share in 2005 even in the face of growing 
            competition from other marketers and the non-sustainable market 
            conditions in 2004.<br>
            <br>
            One of the main achievements of Al Maha was the bagging of the 
            prestigious PDO contract to manage seven PDO locations for a period 
            of 5 years that commenced in May 2004. Al Maha has been able to 
            retain this business since May 2000 principally due to its proven 
            record of the safety measures adopted and competitive prices.<br>
            <br>
            In the line of aviation business, there has been a substantial 
            increase of more than 60 per cent in the commercial aviation sales 
            in 2004 over 2003, which was attributed to new business and 
            non-sustainable market conditions. This has seen an increase in Al 
            Maha’s overall market share in Seeb International Airport to 35 per 
            cent. This is quite an achievement for Al Maha as it was 
            accomplished in a short span of four years since the company started 
            aviation operations in September 2000. The aviation commercial sales 
            in 2005 are expected to remain at the same level of 2004 despite the 
            non-sustainable market conditions in 2004.<br>
            <br>
            <b>Staff, safety and benefits</b><br>
            Training staff to be attentive and wary to avoid any unfortunate 
            incidents is one of the high points of Al Maha. Health and safety 
            manuals have been introduced and are also maintained by Al Maha for 
            the benefit if employees and customers, an endeavour that gained the 
            firm a lot of popularity. <br>
            <br>
            The company continues to bring to the fore of their operations 
            committed and talented people. Al Maha has been and continues to be 
            in the process of developing a high calibre of employees, to ensure 
            a culture of high performance. The company provides high quality 
            training for employees to help keep up to date with emerging 
            technologies, and instil in them strong management and leadership 
            competencies, as well as to provide training for continual career 
            development.<br>
            <br>
            One of the main objectives of Al Maha is to uphold the government’s 
            initiative in promoting Omani nationals in the job sector. Al Maha 
            reached 83 per cent of Omanisation at the end of 2004.<br>
