<img border="0" src="marketing.jpg" align="right" width="120" height="143"></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">B</font><font face="Verdana" size="1">logging is the latest marketing buzzword 
and according to those in the know, it’s revolutionising the media industry. 
Complete with its own, unique lexicon, blogging is, without doubt, the new kid 
on the media block. The verb is to blog and someone who participates in blogging 
is a blogger. <br>
<br>
If you’re part of the blogging community, you’re also part of the blogosphere 
and for those who spend any amount of time on the Net, it’s apparent that 
blogging is becoming increasingly difficult to ignore. Companies the world over 
are starting to use blogs as a communication and marketing tool. In fact, 
blogging has become so important that even Bill Gates has launched one.<br>
<br>
<b>What’s a blog anyway?</b><br>
Simply put, it’s a series of updated posts on a Web page in the form of a diary 
or journal, often including commentary on, and hypertext links to, other 
websites. If you will, it can be another way to publish information. Entries are 
generally short, informal and posted daily. Blogs tend to be highly 
personalised, a type of digital diary. <br>
<br>
From a historical perspective, the 9/11 attacks fuelled the US public’s appetite 
for information, analyses and news. Bloggers rose to prominence by feeding this 
desire. Today, blogs cover a whole range of topics from Joe Consumer’s daily 
diary to in-depth discussions on Internet gaming, health risks associated with 
mobile phone masts to politics and culture and everything in between. For those 
of you who haven’t visited a blog, here are some interesting ones to kick off 
with: <br>
<br>
• http://blogarama.com<br>
• http://blogs.sun.com/roller/main.do <br>
• http://boingboing.net<br>
• http://volokh.blogspot.com<br>
• http://weblog.infoworld.com/udell<br>
• http://blogherald.com/<br>
<br>
<b>Rise to power</b><br>
But why is blogging becoming so popular? Indeed, why are online readers 
deserting traditional sources of news in favour of blogs? Probably because they 
provide an alternative perspective, first-person experiences and interactivity – 
readers are looking for a balanced or more personal angle to their news or Net 
searches or just enjoy looking in on someone else’s news and views.<br>
<br>
Researching information, finding answers and making contacts are the main 
reasons businesses use the Web. A blog can help this process, resulting in 
online networking opportunities. Having a business blog can be a good way for 
your customers to communicate with you, as well as between themselves and, in 
the process, form new relationships. A well-written, frequently updated blog can 
help your business gain credibility as an expert in its field, develop an 
affinity with potential customers and allow you to respond to current issues 
related to your business in an informal manner.<br>
<br>
<b>Biz blogs and search engines</b><br>
A company blog can also provide a number of benefits for your search engine 
traffic. It’s a great way of creating more content on your Website that can be 
used to target keywords relevant to your business – any other Websites that link 
to the content on your blog can benefit your Website’s link popularity. For 
reference, search engines tend to look for frequently updated Websites on a more 
regular basis, which can be of help when you’re trying to develop the search 
engine optimisation on your Website. <br>
<br>
Indeed, some tech savvy media companies have introduced ‘blog monitoring’ where 
they trawl the Net to advise clients on how their brand name is being talked 
about online. The idea behind this emerging service industry is simple – while 
there were 130,000 sites four years ago, today there are an estimated 10 
million. These blogs can make or break a company’s reputation because they 
provide links to one another and allow people to comment on postings. In other 
words, blogging is a massive image-making network that can’t be ignored. <br>
<br>
<b>Why businesses blog</b><br>
A successful blog needs to be updated frequently. This requires time and a 
talent for writing. However, done well, it’s a cost-effective marketing tool 
that can build on the strengths of your existing Website. Moreover, it gives you 
the opportunity to share your opinions, ideas, insights, products and services.<br>
<br>
Businesses the world over have taken to using the Internet to publish their 
thoughts for the same reason their CEOs make public speaking appearances – to 
build credibility and to become recognised as a resource, usually with the 
expectation that it’ll lead to sales. Blogs allow you to quickly and easily 
reach people who aren’t in your direct network and greatly expand the number of 
people you and your company reach. You can also use your blog as a method of 
introducing the people behind your business to your customers, giving your firm 
a more personalised feel which will give you an edge on larger corporations. <br>
<br>
Blogs can be used to run daily or weekly contests, which is another great way to 
keep your customers coming back to your Website. You can also highlight new 
business promotions with your blog. Why not use it to test new ideas or get 
feedback on new products? Photos are an excellent Internet marketing tool to use 
on blogs, having a picture and a short description of the product or service can 
be a lot more effective than writing a descriptive piece highlighting the item. 
In brief, the marketing possibilities of blogging are endless. </font></p>

