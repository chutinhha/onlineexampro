            <span style="font-size: 16pt; font-weight: 700">Lo behold the 
            Armada!</span></font><font face="Verdana" size="1"><b><br>
            <br>
            A Trojan horse in its true sense, the new Nissan Armada is a dream 
            come true when it comes to SUVs, writes Anne Kurian</b><br>
            <br>
            <img border="0" src="car_r.jpg" align="right" vspace="1" width="300" height="133">The 
            Nissan Armada is big. When I say big, I mean gargantuan. The newest 
            SUV to hit the roads, Armada is intimidating in size. And that’s the 
            first aspect you notice about the SUV, which is a workhorse in all 
            senses. It can easily manage a large family off for camping on 
            rugged terrains, double up as a mini-school bus or even pull another 
            Armada with ease. The Armada is a delight for the roads. <br>
            <br>
            Among the SUVs present in the market today, the Ford Expedition 
            would almost match up to the Armada’s size. Even with taking on 
            people to its full capacity and their gear, the Armada does not 
            leave you feeling cramped. <br>
            <br>
            Launched in April, the Armada is part of Nissan’s largest SUV line 
            up in the region. The Armada has space, storage and power as it 
            keywords. And taking in the numerous possibilities for outdoor 
            activities in the country, be it dune-bashing, camping or a day off 
            into the mountains, you can be sure that the Armada is one vehicle 
            you can fall back on. Even on choc-a-bloc city roads, the Armada 
            offers easy manoeuvrability. The Armada is the end product of 
            extensive research and special configuration based on the 
            requirements of customers in the region. OER test drove the Armada 
            to find out more about the SUV. <br>
            <br>
            <b>Exteriors</b><br>
            Size matters and Nissan seems to know it well. The Armada is a 
            Trojan horse on the roads and even in the parking lot it had 
            passers-by gawking. What adds to the huge proportions of the vehicle 
            is the styling of the Armada that includes its wide stance and long 
            wheelbase with short front and rear overhangs. The roof rack, which 
            allows for extra storage space, adds height to the already tall 
            Armada.<br>
            <br>
            The Armada has an imposing chrome grille in body colour and 18-inch 
            five-spoke aluminium alloy wheels that add visual strength to it. 
            The front and rear fenders are flared, which accentuate the tyres. 
            The rear door handles are innovatively placed on the C-pillar and 
            closer to the windows instead of down on the door panel. That should 
            say much about the design aspect of the Armada.<br>
            <br>
            The Armada boasts of a wide rear window that can be opened 
            separately and closed with the touch of a button. When the vehicle 
            is huge, the rear door has got to be huge too, but surprisingly is 
            not as heavy as it looks. <br>
            <br>
            <b>Interiors</b><br>
            The Armada is as roomy and comfortable as you would imagine it to be 
            when viewed from outside. Eight people can be comfortably seated in 
            the SUV along with their luggage with no fear of overcrowding. The 
            rich leather seat upholstery gives off a plush feeling and is 
            accentuated by the neat styling of the interiors. The Armada might 
            be huge in size, but that will not deter any driver from getting in 
            behind the wheels. Power adjustable foot pedals make it easy for 
            drivers of any height to get comfy behind the steering. The steering 
            wheel can be tilted to suit the driver’s comfort, so need I say 
            more?<br>
            <br>
            While on the steering wheel, it’s mounted with audio and cruise 
            controls, making it easier for the driver on the roads. After you 
            have taken in the ample space inside the Armada, what catches your 
            attention is the storage space inside the SUV. Nissan is serious 
            when it comes to storage. The centre console is deep enough to 
            accommodate a laptop or hanging file folders. And if you just want 
            to throw in stuff, you might just be surprised by how much can go in 
            to the centre console. <br>
            <br>
            Just in front of the centre console is a closed storage box that can 
            hold two large cups or throw-in oddments. There are also overheard 
            consoles both in the front and just above the second row of seats. 
            And if that’s not enough, there’s also a small compartment just 
            below the rear AC vent. An under floor storage beneath the rear 
            section allows you to stock more knick-knacks. Now if you are 
            talking about serious storage, then just flip down the second- and 
            third-row seats and what you get is immense cargo space. This gives 
            you 97 cubic feet of space, quite a lot of storage facility. You 
            could even fold down the front passenger seat that will provide you 
            with space for a medium ladder.<br>
            <br>
            One of the best features of the Armada that got our attention was 
            the air-conditioning. The powerful AC cooled down the sweltering 
            interiors within minutes of revving up the engine. A cool feature 
            when you take the temperatures into consideration in this part of 
            the world.<br>
            <br>
            <b>Performance</b><br>
            It might be hard to imagine the gigantic Armada to be nimble, but 
            you got to drive it to believe it. Any apprehensions of driving the 
            Armada, like I had, are put to rest within a few moments of taking 
            control behind the wheel. Easy to navigate and manage, the Armada is 
            a dream on the roads. Packed in with a 340-hp 5.6-litre V8 engine, 
            the Armada can take on any terrain with a briskness that left us 
            impressed. <br>
            <br>
            The 5-speed automatic transmission prompted me to take the Armada on 
            all terrains possible in half a day — the highway, uphill to Qantab 
            and on the rugged roads around Qantab. The Armada took to all the 
            terrains with equal ease. There was no doubt that the 57.1 kg-m of 
            torque could deliver instantaneous power at any rpm. The 
            double-wishbone front and rear suspension makes travel smooth and 
            the independent rear suspension enables each wheel to respond on its 
            own to the road surface.</font></p>
