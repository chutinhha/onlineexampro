            <p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
            <b>Safety</b><br>
            The safety features are equally impressive. The rear sonar system 
            emits intermittent beeps within a range of 1.8 metres, which we 
            found out when a Cavalier reverse-parked within a few centimetres of 
            the Armada. The Armada is also equipped with brake assist system in 
            case of panic braking to provide additional stopping power and also 
            anti-lock braking system (ABS). The Armada comes with 3-point 
            seatbelts, including the third-row middle seat. It also has front, 
            side airbags and side curtain airbags. There are two different 
            deployment levels for the airbags and depends on the seat-belt usage 
            and crash severity. <br>
            <br>
            <b>The finishing line</b><br>
            The Armada is just what you would want out of an SUV, be it the 
            space, comfort, power or looks. The roads and the topography of Oman 
            make the Armada one vehicle you could do with. If you are convinced 
            about the Armada, get ready to write a cheque for RO17, 800.</font></p>
