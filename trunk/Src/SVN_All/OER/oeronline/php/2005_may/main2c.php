<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">The 
small business edge</font><font face="Verdana" size="1"><br>
<br>
<b>The technical assistance programmer by the International Finance Corporation 
for the Middle East and North Africa will be beneficial for small and medium 
sized enterprises as it provides training on how to run and sustain ventures 
profitably. Saad Sabrah, IFC’s business development officer speaks to OER.</b><br>
<br>
<img border="0" src="main2c.jpg" align="left" hspace="4" width="120" height="97"></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">O</font><font face="Verdana" size="1">man’s 
small and medium enterprises (SME) are going to benefit from the International 
Finance Corporation’s (IFC) technical assistance programme for Middle East and 
North Africa (MENA) region. The premier international lending institution, which 
is part of the World Bank group, is now offering its SME training module (called 
Business Edge) to Omani candidates. <br>
<br>
The technical assistance scheme, which is offered under the Private Enterprise 
Partnership in the MENA (PEP-MENA) programme, is aimed at supporting local 
training organisations to meet the required SME management training needs by 
providing them with business edge. IFC is associated with NIIT, which is 
represented by Khimji Training Institute in Oman, to deliver its training 
module. <br>
<br>
IFC’s Business Development Officer Saad Sabrah spoke to OER about his 
institution’s strategy as also the benefits of the whole scheme to the country 
in general and the business community in particular. <br>
<br>
<b>How does IFC’s Business Edge programme help countries like Oman, where 
creating employment opportunity for locals is a priority?</b><br>
We do understand the emphasis the Oman government lays on creating employment 
opportunity for nationals in the private sector. This is a curriculum that 
provides business management training in a very simple format. One of the key 
advantages is that the whole programme is offered in Arabic, which is of world 
standard. <br>
<br>
It provides 38 workbooks in many functional areas, including accounting and 
finance, marketing, budgeting and planning, human resource development, quality 
assurance and management of time. The programme for Omani candidates will depend 
on the special requirement of the local business community. We are in the 
process of translating these workbooks into Arabic, which will help locals to 
understand how to do business. By June, we will have all these books translated 
into Arabic. <br>
<br>
<b>How do you evaluate the success of the programme in countries where it has 
already been implemented?</b><br>
It was a tremendous success in China and Vietnam. IFC was able to sell 40,000 
Business Edge books and trained 5,000 people in China. Similarly, 100,000 
Business Edge books were sold with 7,500 people receiving training in Vietnam. 
The response we have received from training institutes in Egypt so far is very 
positive. Case studies of enterprises in Egypt are relevant to countries like 
Oman. <br>
<br>
<b>How many aspiring business professionals will get training under the Business 
Edge programme in the Sultanate? </b><br>
We have a master franchise agreement with India-based NIIT (represented by 
Khimji Training Institute in the Sultanate) for a period of three years in 
organising the programme in the GCC and Yemen. As it has just started in the 
Sultanate, the local institute is yet to determine response from local 
companies. Under this programme, IFC will train the trainers of the local 
institute, provide Business Edge workbooks and trainer manuals. <br>
<br>
<b>What is your opinion of the capabilities of small and medium-sized 
enterprises in GCC countries? </b><br>
We have not conducted any survey on the capabilities of small and medium sized 
enterprises in the Gulf region. However, enterprises in the United Arab Emirates 
(UAE) are matured when compared to their counterparts in other GCC countries. 
Generally, GCC companies have achieved progress in terms of financial strength 
but these firms are lacking in other aspects like corporate governance. Business 
Edge programme can help in this respect. <br>
<br>
<b>What are the challenges faced by IFC in promoting the programme with the help 
of local training institutes in the Middle East?</b><br>
I would say that cost sensitivity is a major challenge faced by local training 
institutes in promoting it. We also find it slightly difficult to effectively 
promote the programme among potential entrepreneurs. </font></p>

