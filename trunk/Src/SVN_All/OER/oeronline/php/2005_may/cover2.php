            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Even 
            as BankMuscat has, for the fourth year now, stayed at the No. 2 slot 
            in the esteemed The OER 20 rankings, the bank’s performance has 
            improved from RO7.75 million net profits in 2001 to RO34.01 million 
            in 2004 – a jump of more than 114 per cent in a span of four years. 
            Not bad for an entity that has primarily grown in strength through 
            the M&amp;A route</b></font></p>
            <p align="justify" dir="ltr">
            <img border="0" src="abdulr.jpg" align="left" vspace="2" width="200" height="250"></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">W</font><font face="Verdana" size="1">hichever 
            way you look at it, BankMuscat is the largest bank in the country. 
            It commands a market share of 36.7 per cent in terms of total 
            assets, 37.6 per cent in terms of total credit and 33.3 per cent in 
            terms of total customer deposits. The bank’s share of total savings 
            deposits was 36.9 per cent as at 31 December 2004. The icing on the 
            cake? The country’s biggest bank got even bigger last year – it 
            achieved a net profit of RO34.1 million during the year as against 
            that of RO27.1 million for 2003, a jump of an imposing 25.8 per 
            cent. <br>
            <br>
            BankMuscat, whose total income grew by 4.79 per <br>
            cent, from RO120.74 million in 2003 to RO126.52 million in 2004, 
            would have been an even more massive entity today had it not been 
            for a failed merger attempt with National Bank of Oman (NBO; The OER 
            20 rank for 2005: #7; income for 2004: RORO56.68 million). Merger 
            talks between the banks were called off over differences on the 
            value of NBO, Oman’s second-largest bank. The merger, proposed in 
            September, was delayed earlier this year when BankMuscat said it 
            needed more time to review NBO’s bad-loan provisions.<br>
            <br>
            ‘‘In the absence of an agreement being finally reached on the 
            consideration to be exchanged between the two organisations, the 
            merger talks had to be prematurely called off,’’ said Sheikh 
            AbdulMalik bin Abdullah Al-Khalili, the bank’s chairman. ‘‘The NBO 
            merger breakdown, due to revaluation problems, was unfortunate but 
            as always, we will welcome any initiation to merge with another bank 
            to strengthen our business,’’ agrees AbdulRazak Ali Issa, the CEO of 
            BankMuscat. <br>
            <br>
            However, he claims that ‘‘merger talks with NBO have always been 
            accorded the highest level of importance.’’ The merger would have 
            presented BankMuscat the best opportunity so far for a much-awaited 
            entry into the UAE market, through NBO’s existing operations in Abu 
            Dhabi. This, of course, would have gone a long way in realising 
            BankMuscat’s regional ambitions. <br>
            <br>
            <b>Beyond borders</b><br>
            The bank’s regional ambitions, however, were no way put on the 
            backburner as its international operations saw considerable 
            activity. BankMuscat exercised its full option to maintain its 
            strategic stake in Centurion Bank Ltd (CBL) in India. However, the 
            party was somewhat disrupted by the fact that the bank had to absorb 
            a net loss of RO1.42 million for 2004 from CBL, which, as part of 
            its restructuring, recapitalisation and clean up plan, reported a 
            loss for the year ended March 31, 2004. The bank’s share of loss was 
            RO2.07 million. <br>
            <br>
            However, ‘‘having effected a total clean up of the balance sheet as 
            of March 31, 2004, CBL has now announced a profit for the 9 months 
            ended December 31, 2004,’’ informs Sheikh AbdulMalik. The bank’s 
            share in the profit amounted to RO0.65 million. ‘‘We are confident 
            that CBL is now well positioned to achieve sustained business 
            momentum and profitability with a strong new management team and 
            strategy in place,’’ adds the chairman.<br>
            <br>
            ‘During the year, the bank was also granted the necessary regulatory 
            approvals to set up a branch in the Kingdom of Saudi Arabia. ‘‘We 
            will start doing business in (the Saudi capital) Riyadh by the end 
            of 2005 with the opening of our branch there,’’ confirms Issa.&nbsp; 
            The end of 2004 saw the bank deciding to convert its erstwhile 
            Bahrain operations into an independent entity named BankMuscat 
            International (BMI) effective January 1, 2005. BMI has been set up 
            in association with strong regional partners and is envisioned to 
            become a strong regional bank in future. <br>
            <br>
            <b>Number jumbo</b><br>
            Notwithstanding the fact that the proposed merger did not eventually 
            materialise, 2004 was an extremely eventful year for the bank. ‘‘The 
            year was amongst the busiest that the bank has seen in the recent 
            past,’’ claims Al-Khalili. The bank’s non-interest income grew from 
            RO19.8 million in 2003 to RO22.8 million in 2004 – an increase of 
            15.1 per cent. The operating profit of RO55.3 million in 2004 is 
            13.1 per cent higher than RO48.9 million achieved in 2003. At the 
            same time, operating expenses too rose by 7.7 per cent – from RO39.7 
            million incurred in 2003 to RO 42.7 million in 2004. Thus, the Cost 
            to Income ratio showed a slight improvement from 44.7 per cent in 
            2003 to 43.5 per cent in 2004.<br>
            <br>
            The bank’s return on average assets improved from 1.75 per cent as 
            of December 31, 2003 to 2 per cent as of December 31, 2004. The 
            return on average equity improved from 21.5 per cent to 21.8 per 
            cent during the same time. The basic earnings per share also 
            improved from RO0.498 in 2003 to RO0.570 in 2004. The bank’s net 
            loans and advances portfolio grew by RO85 million to RO1,329 million 
            in 2004 compared to RO1,244 million in 2003. <br>
            <br>
            The bank’s customer deposits grew by RO83 million to RO1,162 million 
            in 2004 while savings deposits increased by 21 per cent to RO287 
            million. The bank also issued RO29.8 million worth of bonds with a 
            coupon rate of 6.25 per cent during the year. BankMuscat has 
            announced a cash dividend of 25 per cent and a stock dividend of 15 
            per cent for the year 2004. Around the year-end, the bank, in 
            association with India’s SBI Capital Markets, was also retained as 
            advisor to the Government of Oman for the first proposed Independent 
            Water Project to be set up in the country. <br>
            <br>
            ‘‘While the year saw the issuance of the bank’s maiden Euro Medium 
            Term Note programme and the second issuance of the Bank’s 10-year 
            Rial Omani bonds, it also saw the bank winning a series of mandates 
            in the power sector and being awarded the prestigious Omantel 
            privatisation mandate,’’ adds Sheikh AbdulMalik. On the retail side, 
            BankMuscat further diversified its revenue stream by introducing 
            bancassurance—underwritten by National Life and Norwich Union—in the 
            country.<br>
            <br>
            <b>Rave reviews</b><br>
            Otherwise as well, the bank continued to harness the power of 
            technology to innovate and introduce products and services that meet 
            the current and emerging need of customers. ‘‘The bank’s emphasis on 
            improving quality and service standards across business lines and 
            departments bore rich dividends during the year as BankMuscat earned 
            the rare distinction of becoming the first bank in the Middle East 
            region to be completely ISO:9001 certified,’’ beams Sheikh 
            AbdulMalik.<br>
            <br>
            The bank continued to make a favourable impression on various awards 
            juries during the year and was adjudged the Best Bank in Oman by 
            Euromoney for the third successive time and by Global Finance and 
            The Banker, Financial Times for the fourth consecutive time. Other 
            accolades that came the Bank’s way during the year included: Best 
            Retail Bank and Best Corporate Bank in Oman (Banker Middle East), 
            Best Consumer Internet Bank (Global Finance), and Best Trade Finance 
            Bank &amp; Provider and Best Forex Bank &amp; Provider in Oman (Global 
            Finance). ‘‘Perhaps the most cherished award of the year was the 
            People Development Award that the Bank won at the Oman Awards of 
            Excellence last year,’’ acknowledges Sheikh AbdulMalik.<br>
            <br>
            <b>Outlook 2005</b><br>
            The strong performance of the bank and the milestones achieved 
            during the past year will once more set a foundation for future 
            growth during 2005. In fact, the challenges and possibilities that 
            lie ahead over the next 12 months promise to make the year one of 
            the most significant in the bank’s young history.<br>
            <br>
            ‘‘Central to achieving our vision of becoming one of the foremost 
            banks in the region by the turn of the decade is the continuing and 
            unstinted support and patronage of the bank by our family of valued 
            customers, shareholders, employees, business associates, 
            well-wishers and the community in which we operate. The bank remains 
            firm in its commitment to provide each one of these segments with 
            the greatest possible value by pursuing its overarching goal of 
            excellence and by trying to meet each and every current and emerging 
            need of the discerning customer, both in Oman and across the 
            countries in which we operate,’’ concludes Issa.<br>
