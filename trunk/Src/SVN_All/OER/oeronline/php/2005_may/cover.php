            The chairman of Oman Cables, Mustafa bin Mukhtar bin Ali Al Lawati, 
            explains in his annual report to the shareholders that major 
            expansion project of Medium Voltage Cables (MV Cables) was completed 
            by the end of 2003; production of MV cables started in 2004. With 
            that, the company has been able to launch the new product range of 
            MV Cables successfully both in the local and international markets. 
            Al Lawati adds that the year 2004 was a year of tremendous increase 
            in the prices of the raw materials worldwide. This naturally 
            resulted in increase in the selling price of cables. <br>
            <br>
            OCI's sales have increased from RO22.16 million in 2003 to RO36 
            million in 2004, an increase of 62 per cent compared to the previous 
            year. International exports helped in expanding OCI’s marketing 
            activities considerably. The net profit before tax is RO1.555 
            million compared to RO0.764 million in 2003, an increase of 103 per 
            cent compared to 2003. In line with the guidance issued by the 
            Capital Market, the board has recommended a dividend of 20 per cent 
            subject to the approval of the Annual General Meeting. <br>
