            share growth companies. Al Jazeera Tube Mills at the No. 1 spot, 
            Oman Aviation Services, National Bank of Oman, and Oman Cables 
            Industry (OCI) are the newcomers replacing ONIC Holding, Salalah 
            Port Services, OMINVEST, and Oman Cement. Again, similar to the 
            Growth of Profits listing, featuring for two successive years in the 
            EPS Growth listing is an honourable accomplishment. This commendable 
            performance was turned in by Renaissance Services at No. 2 (No. 1 
            last year).<br>
