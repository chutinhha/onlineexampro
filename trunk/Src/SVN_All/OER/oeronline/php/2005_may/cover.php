            the first time since this review started being published, only three 
            of The Top 5 most profitable companies in Oman are banks. It is 
            encouraging to note that all five companies are showing growth in 
            profits for the year. There has been one change in the top five 
            companies by profit. Ominvest, which was in fifth place last year, 
            is out. Renaissance Services has leapfrogged to the No. 3 spot from 
            No. 9 last year. BankDhofar has slipped to the No. 5 position after 
            being at No. 3 last year. The top two companies remain unchanged 
            from last year. <br>
            <br>
            BankMuscat was once again the most profitable company in Oman as 
            well as the second largest company in Oman based on turnover for the 
            year 2004. BankMuscat has maintained this position by attaining a 
            growth in profit for the year of about 26 per cent. The chairman 
            Sheikh AbdulMalik bin Abdullah Al-Khalili states in his report to 
            the shareholders that the year 2004 was amongst the busiest that the 
            bank has seen in the recent past. On the retail side, Sheikh 
            AbdulMalik explains that the bank took a significant step in its 
            vision to diversify its revenue streams by introducing bancassurance 
            in Oman. The bank’s international operations also saw considerable.
            <br>
            <br>
            Sheikh AbdulMalik goes on to say that BankMuscat commands a market 
            share of over 36.7 per cent in terms of total assets, 37.6 per cent 
            of total credit and over 36.9 per cent of total customer deposits. 
            The board has proposed a cash dividend of 49 per cent for the year 
            2004. the chairman confirmed that BankMuscat was on track to 
            achieving the vision of becoming one of the foremost banks in the 
            region by the turn of the decade.<br>
