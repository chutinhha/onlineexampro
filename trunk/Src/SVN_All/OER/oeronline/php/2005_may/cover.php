            companies for the year 2004.<br>
            </font></b>
            <font face="Verdana" style="font-size: 10pt">
            <b>
            <br>
            <img border="0" src="cover2.jpg" align="left" hspace="3" vspace="2" width="150" height="82"></b></font><b><font face="Verdana" size="2">T</font></b><font face="Verdana" size="1">he 
            year 2004 continued to be positive for Oman’s economy in general and 
            significantly bullish for the Muscat Securities Market (MSM) in 
            particular. The price of oil continued to increase during the year. 
            Oman crude realised an avera</font><font face="Verdana" size="1">ge 
            price of $33.9 a barrel in 2004 compared with the budgeted price of 
            $21 a barrel. This price increase more than compensated the fall in 
            oil production from 824,000 barrels per day to 763,000 bpd. 
            Moreover, the year 2004 also saw a continuing increase in income 
            from LNG. This resulted in a reversal of the planned budgeted 
            deficit to a surplus.<br>
            <br>
            The MSM also continued to perform well during the year 2004. Cash 
            liquidity during the year eased and liquidity of stocks improved 
            substantially. New IPOs like Al Maha Petroleum, Dhofar University, 
            Al Kamil Power and AES Barka added flavor to the services sector of 
            the market and helped improve market activity. Trading volumes 
            amounted to RO763 million in 2004 compared to RO593 million in 2003, 
            an increase of 29 per cent. <br>
            <br>
            The General Price Index was renamed the MSM 30. The Index was also 
            re-based to 1,000 in line with other international indices and 
            reconstituted to reflect the market capitalisation more 
            appropriately. The adjusted benchmark MSM 30 Index ended the year to 
            close at its 4-year high of 3,375.05 points, a gain of 23.78 per 
            cent for the year. The top performer was the Industry Sector, which 
            was up 34.7 per cent followed by the Banking and Investment Sector, 
            which gained 23.9 per cent and the Services Sector ended with an 
            increase of 12.7 per cent for the year. Foreign participation in the 
            MSM also registered an increase during the year.<br>
            <br>
            The government continued its emphasis of diversifying the economy 
            away from dependence on oil. Several mega projects were launched 
            including the Sohar Petrochemical plant, Sohar Power &amp; Desalination 
            plant, a fertiliser plant and several other large projects. The 
            government continued to pursue its privatisation policy. A second 
            mobile licence was awarded to Nawras Telecom. <br>
            <br>
            Substantial progress has been made during the year 2004 with regard 
            to tourism. A separate ministry of Tourism was created during the 
            year. The Wave Project was launched. Several other tourism related 
            projects are in the pipeline. The easing of visas had a major impact 
            on the hotel and travel industry in this year. Hotels were fully 
            booked for the better part of the year and, for the first time in 
            Muscat, one had to make reservations in advance to get a table at 
            some of the better restaurants in town.<br>
            <br>
            On the international scene, the investment landscape last year 
            showed resilience in the face of unrest. Throughout the year, the 
            strength of US corporate earnings provided support to the market 
            while concerns about escalating oil prices, the falling dollar, and 
            rising interest rates created intermittent uncertainty, much of 
            which seemed to dissipate at year-end. In the end, 2004 proved to be 
            a reasonable year, with positive returns in both the stock and bond 
            markets. A late-year surge boosted the returns of hedge funds as 
            well as US stocks, with fourth quarter gains driving most of the 
            calendar year 2004.<br>
            <br>
            During the year 2004, the revenues of Oman’s 20 largest companies 
            showed a handsome growth. Total revenues for The OER 20 went up by 
            8.19 per cent to RO1,016 million. Corporate performance of the year 
            2004, overall, was also extremely positive. The profits for the year 
            2004 increased dramatically by 186 per cent to RO132 million from 
            the figure of RO46 million last year. The main reason for the dismal 
            performance of last year was the substantial loss of RO52 million 
            posted by National Bank of Oman (NBO), which completely distorted 
            the overall figure in 2003.<br>
            <br>
            The total market cap of The OER 20 on December 31, 2004, was RO1,560 
            million, which was up by 23 per cent compared to 2003. On April 20, 
            2005, the market cap of The OER 20 was once again substantially up 
            to RO2,011 million. The OER 20 companies represented 44 per cent to 
            the total market cap of the MSM of RO3.587 billion at the end of 
            2004. The average P/E ratio of The OER 20 based on the profits of 
            the year 2004 and the share price on April 20, 2005, is 15.28 times 
            earnings. <br>
            <br>
            Who’s <b>in, </b>who’s<b> out?</b><br>
            We have two newcomers on the list this year. Al Maha Petroleum comes 
            straight into the No. 3 spot and Al Jazeera Tube Mills is at No. 15. 
            These companies have made it to The OER 20 this year at the expense 
            of United Power Company and Oman National Investment Holding Company 
            simply because the newcomers had higher turnovers. The ranking of 
            Oman’s 20 largest companies in order of revenue produces a list, 
            which includes the five major banks, one insurance company, seven 
            companies from the industrial sector and seven companies from the 
            services sector. <br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">here 
            has been one change in The Top 5 companies in Oman. Al Maha 
            Petroleum has replaced NBO, which was in the third place last year. 
            Oman Oil has moved up one place and Oman Aviation has slipped one 
            place. The top two companies remain unchanged from last year. Shell 
            Oman Marketing (SOM) continues to remain the largest public company 
            in Oman, but BankMuscat seems to be swiftly catching up. SOM has 
            once again this year shown a negative growth in revenue of –12.75 
            per cent. <br>
            <br>
            Dr Andrew Wood, the chairman of SOM, in his report to the 
            shareholders explains that 2004 has seen an intensifying level of 
            challenge in the market. Dr Wood outlines a number of key aspects of 
            the business environment that have either contributed positively to 
            the results or have posed a significant challenge.<br>
            <br>
            Dr Wood explains to the shareholders the outlook for year 2005 is 
            generally strong, given the encouraging economic scene in the 
            Sultanate. In their own business, although the regulatory 
            authorities have initiated processes to scrutinise licensing of new 
            retail service stations, the aggressive network expansion in the 
            industry will continue to be a major threat. The company’s ability 
            to invest will be fully focused upon such investments opportunities 
            where long-term shareholder value growth can be achieved. <br>
            <br>
            The key growth areas are constantly under observation to augment any 
            demand gaps arising due to demographic and road network 
            developments. The major construction project activity is also 
            expected to provide increased demand although SOM expects increasing 
            levels of price competition to secure this volume.<br>
