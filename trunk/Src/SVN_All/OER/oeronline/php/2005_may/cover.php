            average growth of the MSM index of 23.78 per cent. Interestingly, 
            the top four companies on this list are newcomers and only Oman 
            Cement retains its position at No. 5 from last year. Al Jazeera Tube 
            Mills is on top of the list of companies registering the best share 
            price growth in the year 2004.<br>
            <br>
            Samir Fancy, the Chairman of Topaz Energy &amp; Marine, in his report to 
            the shareholders states that 2004 was a very successful year for 
            company. Fancy explains that the company continues to progress well 
            and recorded a net income of RO2.507 million (18 per cent) on sales 
            of RO24.652 million (11 per cent) in 2004. This compares very 
            favourably with 2003 when the results were RO2.125 million and 
            RO22.172 million, respectively. EPS increased to RO0.143 (RO0.112 in 
            2003) for the year. <br>
            <br>
            Fancy adds that as a result, the board is pleased to recommend a 
            dividend of 10 per cent (2003 @ 9 per cent) for the year 2004. Fancy 
            advises that all TEAM divisions performed well for the year. 
            Overall, the group saw healthier operating margins and benefited 
            from the focus on efficiency improvements. It is significant that 
            these results have been derived from operations alone unlike other 
            years where sales of ships have added to business income. This is a 
            very satisfactory situation and indicates the potential to enhance 
            earnings in the future. <br>
            <br>
            Fancy explains that significant milestones in 2004 include the 
            commencement of construction of new distribution facilities for our 
            stainless steel trading business (Mezon Trading) in the Jebel Ali 
            Free Trade Zone and for Adyard’s maintenance services division in 
            Abu Dhabi. The new facilities will enhance the company’s ability to 
            service customer requirements in the future. The acquisition of the 
            assets of Fujairah Marine Services in Q4 will enable Nico 
            International, the Ship Repair Division, to increase market share 
            and expand into new services in the strategically important eastern 
            seaboard of the UAE. <br>
            <br>
            The company continues to strengthen their asset base with the 
            acquisition of two second-hand AHTS vessels at competitive prices in 
            Q4. The vessels are expected to join the Nico World fleet in Q2 
            2005. Looking forward, Fancy explains that the oil &amp; gas market 
            remains strong and the outlook for all TEAM operating companies is 
            positive. Growth through acquisition is part of the strategy. TEAM 
            embarked on an acquisition earlier last year but after a detailed 
            evaluation felt that the deal would not add the required value. <br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">hree 
            new companies have entered the ranking of the best five dividend 
            yield companies. OMINVEST, Oman Flour Mills and Oman Oil are the 
            newcomers replacing United Power Company, Oman Refreshments, and 
            OIB. Shell Oman Marketing has jumped from the No. 5 spot last year 
            to the No. 1 spot and Areej Vegetable has slipped from No. 2 to the 
            No. 4 spot. <br>
            <br>
            The chairman of OMINVEST, Taya Bin Jandal Bin Ali, has stated in his 
            report to the shareholders that this is the company’s 20th year of 
            operations. The results of the company speak for themselves; good 
            profits have been earned over the years, despite frequent volatility 
            and turmoil in local and overseas markets and shareholders have been 
            rewarded by continuous and uninterrupted dividend payments. 
            Shareholders have, since inception, been paid cash dividends of 
            RO39.05 million (including the 2004 proposed dividend) against the 
            RO12 million capital contribution by them in cash in 1984 (RO8 
            million) and 1994 (RO4 million). The Company also issued a RO3 
            million stock dividend in 1997 which brought the paid-up share 
            capital to RO15 million. The shares, on the date of the report, are 
            trading at around RO 4.750, giving a value of RO71 million for the 
