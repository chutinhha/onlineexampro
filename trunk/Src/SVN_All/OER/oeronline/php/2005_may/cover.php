            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">A</font><font face="Verdana" size="1">l 
            Jazeera Tube Mills has shown the highest growth in profits by a 
            staggering 840.17 per cent and has jumped straight to the No. 1 
            spot. Four companies that were in The Top 5 last year, ONIC Holding, 
            OMINVEST, Salalah Port Services, and Oman Cement Company have moved 
            out. It is indeed, a distinction to feature in this list for two 
            consecutive years – a feat achieved in last year’s rankings by 
            OMINVEST. This year, Renaissance Services at No. 2 (No. 1 last year) 
            has managed this achievement. Oman Aviation Services at No. 3, 
            National Bank of Oman at No. 4 and Oman Cables Industry at No. 5 are 
            the other three new companies that have made it to this list.<br>
            <br>
            Ahmed bin Mohamed bin Omeir Al Hinai, the chairman of Al Jazeera 
            Tube Mills, in his report to the shareholders states that, in 2004, 
            the steel industry enjoyed one of its best years ever. The industry, 
            which has suffered from an oversupply for a number of years, saw a 
            change in its fortunes from mid-2002, when a balance between demand 
            and supply was being experienced; thus, this resulted in a steady 
            improvement in prices of all steel products. In 2002, the only major 
            economy that pushed up demand and therefore pushed up steel prices 
            was China. That was the reason for the minor improvement in steel 
            prices. The improvement in late 2003 and early 2004, pushed demand 
            to new height resulting in the unprecedented acceleration in the 
            steel prices witnessed in 2004.<br>
            <br>
            Al Hinai adds that the company explored the possibilities and 
            maximised on the opportunities that this dramatic situation offered. 
            It was realised early on that this situation will put tremendous 
            strain on the working capital required to purchase this much more 
            expensive raw material. But the company was fortunate in receiving 
            the timely support of their bankers. As a result, Al Jazeera 
            actually increased its operational size. Al Hinai advises that the 
            Ministry of Commerce &amp; Industry has approved to grant government 
            soft loan for setting up of fourth tube mill to manufacture pipes 
            ranging from 2” NB to 6’’ NB. The expansion project is in progress 
            and is expected to commence the trial production by end 2005.<br>
            <br>
            Further, as announced a few months ago, the company will start a 
            sub-contractual manufacturing relationship in Saudi Arabia. If this 
            is successful, it will be a model for other ventures. Sheikh 
            AbdulMalik explains that, during the year, the shareholders in the 
            ordinary general meeting held on September 25, 2004, duly approved 
            an interim stock dividend in the ratio of 1:2 equity shares and 
            accordingly the issued and fully paid up capital increased by 
            1,200,000 of RO 1/- each in the year. Further, he states that the 
            directors recommend a final stock dividend of 70% of the issued and 
            paid-up capital. <br>
            <br>
            It is important to understand that, with all these expansion that 
            the company has undertaken, the company will require substantially a 
            larger capital base to fund and finance it, and this is the reason 
            we recommend this increase in the capital of the company.<br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">F</font><font face="Verdana" size="1">our 
            of the top five companies that have the highest amount of equity 
            employed are banks. There is no change in this list from last year. 
            Oman International Bank (OIB) is the second highest capitalised 
            company in Oman. Reem Omar Zawawi, the chairperson, states in her 
            report to the shareholders that the year 2004 was a productive year 
            for the bank with several major achievements. The bank started the 
            year with celebration of its 20th Anniversary. Their focus during 
            the year was consolidating their position in the local market, 
            increasing their market share, enhancing asset quality while 
            reducing operating expenses. In addition, the emphasis of continued 
            improvement of customer service was a major priority. <br>
            <br>
            Zawawi states that OIB’s performance for the year has been, once 
            again, very encouraging. Business continues to perform well, driven 
            by the excellent momentum in retail, corporate and treasury 
            activities. As a result of strong growth in the loan book, funded by 
            low cost deposits, the Net Interest Income increased from RO24.698 
            million in 2003 to RO27.493 million in 2004, a growth of 11.3 per 
            cent. Other income for the year 2004 of RO8.898 million showed a 
            growth of 13.2 per cent over the previous year, largely the result 
            of increased volume of retail and corporate business. <br>
            <br>
            Zawawi explains that their efforts to reduce operating expenses 
            continue with several cost rationalisation initiatives undertaken 
            during the year. The results reflect a favourable trend with the 
            Operating Expenses reflecting a reduction of 8.1 per cent from the 
            previous year. The Cost to Income ratio declined to 47.5 per cent 
            for the current year as compared to 56.0 per cent for the previous 
            year. Accordingly, the bank’s Operating Profit increased from 
            RO14.911 million in 2003 to RO19.273 million in 2004, a strong 
            growth of 29.3 per cent. OIB’s commitment to improve asset quality 
            and thereby reduce the provision for loan impairment continues with 
            the specific provision for loan impairment for the year decreasing 
            by 29.6 per cent to RO4.410 million compared to the previous year.
            <br>
            <br>
            Zawawi adds that overall the year was a rewarding one for the bank’s 
            shareholders. In addition to the payment of the cash dividend of 21 
            per cent on the face value of the share, the bank’s share price also 
            appreciated by 43.5 per cent. The bank looks to the future with full 
            confidence and optimism. The prudent macroeconomic policies of the 
            government of the Sultanate will provide ample opportunities for 
            banks to meet the needs of their customers. This, together with the 
            solid business franchise that the bank enjoys in Oman, and the 
            significant market share gained over the past year, leads us to 
            believe that OIB will be successful in expanding and developing its 
            business, and thereby continuing to contribute to the progress of 
            the nation.<br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">O</font><font face="Verdana" size="1">nce 
            again, four of the top five companies in Oman having the highest 
            market capitalisation are banks. The list is the same as last year 
            except that OIB and Oman Cement have moved one place up and NBO and 
            BankDhofar have both slipped a place.<br>
            <br>
            The Chairman of NBO, Suhail Salim Bahwan, in his report to the 
            shareholders states that the outcome of the bank’s operations for 
            the year 2004 resulted in a net profit of RO 5.2 million. This 
            contrasted with a net loss of RO 51.8 million reported for the year 
            ended December 31, 2003. In terms of balance-sheet evolution, the 
            focus in 2004 was cost control and asset liability management with 
            special emphasis on fund management. <br>
            <br>
            Bahwan adds that during the year, additional provisions of RO30.3 
            million were established which are significantly lower than the 
            RO70.7 million established in 2003. Bahwan explains that the actual 
            provision recoveries and releases showed an improvement from RO1.5 
            million in 2003 to RO18.5 million in 2004.<br>
            <br>
            With regard to the proposed merger plans with BankMuscat, Bahwan 
            goes on to explain that in February 2005, the board of directors met 
            to consider the various issues and outstanding matters relating to 
            the proposed merger and after considering the best interest of all 
            the stakeholders and the ongoing delays in reaching an agreement on 
            the outstanding issues it was decided not to pursue with the 
            proposed merger. Bahwan is confident that throughout 2005 and 
            beyond, management will focus on prudently growing the business as 
            well as continue with their remedial efforts which has shown a very 
            promising start in 2004.<br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">I</font><font face="Verdana" size="1">nterestingly, 
            none of the top five companies showing the best Return on Equity 
            Employed come from the banking sector. Renaissance Services 
            continues to retain the top position for the second successive year 
            showing a return on equity of a massive 54.02 per cent. Two 
            companies that were in the Top 5 last year, Salalah Port Services 
            and Oman Oil Marketing, have moved out. Dhofar Insurance and Shell 
            Oman Marketing have slipped one place. Al Jazeera Tube Mills at No. 
            2 and Areej Vegetable at No. 5 are the two new companies that have 
            made it to this list.<br>
            <br>
            Stephen Thomas, the CEO of Renaissance Services, states in his 
            report to the shareholders that the year 2004 was an exceptional 
            year by every measure. Revenue is up from RO45.973 million to 
            RO62.580 million; and Net Profit up from RO4.867 million to RO13.461 
            million. These results have generated strong cash flows enabling the 
            company to reward the shareholders, strengthen the balance sheet, 
            reduce debt and generate resources and financial flexibility to 
            invest in the future growth.<br>
            <br>
            With the exception of the Education &amp; Training Group, which had a 
            breakeven at the operating level, every business in the group has 
            delivered higher profit contributions this year: The Contract 
            Services Group - RO15.7 million; the Technology Group - RO949,000; 
            and the Media Communications Group - RO199,000. Profit contribution 
            of RO835,000 has also been realised in the group’s other 
            investments, including from the Dubai Wire Group (DWG). <br>
            <br>
            Increasing regional penetration is key to the company’s growth 
            plans. The macro-environment in the region is challenging but offers 
            tremendous opportunity to the company’s services businesses.<br>
            <br>
            </font>
