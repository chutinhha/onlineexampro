            <span style="font-weight: 700">
            <font face="Verdana" style="font-size: 16pt">PERFORMANCE PAYS</font></span></p>
            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">The 
            fact that it hasn’t moved any higher than the No. 6 sot in The OER 
            20 this year doesn’t really do justice to the phenomenal growth 
            attained by Renaissance Services. After two exceptionally good 
            years—a cumulative increase of 115 per cent in income and a whopping 
            862 per cent in net profits—sustaining this pace of growth will be 
            the next challenge</font></b></p>
            <p align="justify" dir="ltr">
            <img border="0" src="step.jpg" align="left" vspace="2" width="200" height="250"></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">R</font><font face="Verdana" size="1">enaissance 
            Services has done it again. The company that, in the words of its 
            CEO Stephen Thomas, ‘‘began a strategic realignment’’ behind its 
            four core businesses a little over five years ago, has today emerged 
            as perhaps the best performer on the Sultanate’s corporate horizon. 
            Its income grew 36 per cent to RO62.58 million in 2004 compared with 
            the RO45.97 million that it posted in 2003. To put things in 
            perspective, the 2003 figure entailed a jump of an outstanding 58 
            per cent over 2002 results (RO29.117 million). That, in effect, 
            means that the company has grown an extraordinary 115 per cent in 
            terms of revenues in a span of but two years.<br>
            <br>
            What’s more, the company’s bottomline was augmented by a dynamic 177 
            per cent to RO13.46 million in 2004 compared with net profits of 
            RO4.87 million that it posted in 2003. Again, when you take into 
            account the increase from the RO1.40 million that the company 
            managed in 2002, it works out to a mind-boggling 862 per cent – 
            that’s the stuff legends are made of! But as Thomas will have you 
            believe, it isn’t a fairytale – it is a tale of grit and 
            determination, of perseverance and diligence, one where a company 
            dreamt big, believed in that dream, and then went all-out to fulfil 
            it. <br>
            <br>
            ‘‘At Renaissance, we strive to attain ambitious performance goals 
            with courage and integrity,” maintains Thomas. Prudently, the 
            company has not limited its operations to the geographical boundary 
            of the country, but is leveraging its local presence to clearly 
            establish a strong foothold in the Middle East. ‘‘Our regional reach 
            is critical. Renaissance today is driving performance across eight 
            countries in the region,’’ explains Thomas. ‘‘The 2004 results 
            demonstrate a robust operational performance, which reflects 
            Renaissance’s industry leading position of all its businesses in 
            Oman and a rapidly expanding regional footprint,’’ he adds. <br>
            <br>
            ‘‘Increasing our regional penetration is key to our growth plans. 
            The macro-environment in the region is challenging, but offers 
            tremendous opportunity to our services businesses,’’ echoes Samir 
            Fancy, the chairman of the company. ‘‘These results have generated 
            strong cash flows, enabling us to deliver on our promises to reward 
            the shareholders, strengthen the balance sheet, reduce debt and 
            generate resources and financial flexibility to invest in the future 
            growth of the business.’’ <br>
            <br>
            As the company began a strategic realignment at the turn of the 
            millennium, it simultaneously undertook a spring cleaning exercise 
            in the right earnest, and has ‘‘shed non-core and non-performing 
            businesses, written off historical burdens, unbundled costs and 
            improved efficiency, improved cash flows and reduced debt, created 
            real shareholder value, and restructured ourselves for growth,’’ 
            catalogues Thomas. <br>
            <br>
            Not surprisingly, then, while the company had committed to bring 
            down term borrowings by RO9.4 million in 2004, it actually exceeded 
            that and reduced borrowings by RO11.7 million. ‘‘This has 
            strengthened our position to have the financial resilience to create 
            value-enhancing opportunities,’’ beams Fancy.<br>
            <br>
            <b>Vision 20/20</b><br>
            ‘‘We want Renaissance to be recognised throughout the region as a 
            world-class, internationally competitive, premier service company,’’ 
            proclaims Thomas. ‘‘This shall be achieved through the quality of 
            our customer service; good governance; outstanding HSE, Quality and 
            MIS systems; a sustained growth and profit record; and a proven 
            ability to improve the economic well-being and quality of life of 
            all stakeholders,’’ he adds.<br>
            <br>
            As Fancy puts it rather colourfully, ‘‘one great advantage to being 
            an Omani company with international capability is that we have a 
            foot in two cultures. We have learnt our skills from global 
            expertise, but we are comfortable and at home in the Middle East 
            environment. What looks like risk to some looks like opportunity to 
            us.’’ To be sure, Renaissance companies have indeed carried out work 
            in Bahrain, Iraq, Kenya, Kuwait, Qatar, Saudi Arabia, UAE and Yemen, 
            apart from, of course, Oman.<br>
            <br>
            Moreover, Renaissance is in an expansion phase, and is seeking new 
            opportunities throughout the region and beyond, through partnership, 
            merger &amp; acquisition and competitive tendering. ‘‘Renaissance is a 
            reliable partner for those looking to bring their expertise to Oman. 
            At the same time, it offers world-class expertise to other markets 
            in the region; providing competitive solutions to organisations that 
            want to outsource highly efficient quality services,’’ sums up 
            Thomas.<br>
            <br>
            <b>Core of the competence</b><br>
            Renaissance’s four core businesses—Information &amp; Healthcare 
            Technology; Contract Services; Education &amp; Training; and Media 
            Communications—make it a diversified services company. The company 
            has proved its ability to compete internationally by winning and 
            carrying out major projects abroad, and today is at the threshold of 
            growing even further. <br>
            <br>
            Not surprisingly, as has been the case for the last three years, the 
            company’s Contract Services Group once again surpassed the 
            benchmarks that it itself set in the previous year, and contributed 
            72 per cent of Renaissance’s revenues and an overwhelming 97 per 
            cent of the profits from operations before allocations. CSG posted 
            an income of RO45.105 million (2003: RO31.386 million) and made net 
            profits to the tune of RO15.712 million (2003: RO9.014 million). <br>
            <br>
            ‘‘CSG has once again produced exceptional results. This was achieved 
            by maintaining market leadership performance in the home market and 
            sustaining high levels of business in Overseas Division projects,’’ 
            reveals Thomas. While he did not reveal the details of those 
            projects, citing ‘‘protection of both client information and 
            competitive advantage,’’ what Thomas did let known was that CSG has 
            three successful businesses: short-term contracts, currently 
            TISCO/RCCS in Oman; long-term investment contracts, currently RS PAC 
            Division in Oman; and international projects, operated through RS 
            Overseas Division. <br>
            <br>
            ‘‘The company’s strategy is to consolidate home market leadership by 
            defending and sustaining CSG’s current market share in Oman; 
            implement an aggressive business development plan inside and outside 
            Oman; and continuous development of world-class business systems and 
            organisation resources, especially people,’’ he says. <br>
            <br>
            The year proved to be excellent for the company’s Technology Group 
            as well, whose revenue was up 29.9 per cent – from RO9.377 million 
            in 2003 to RO12.181 million in 2004. TG managed impressive growth 
            for the year with significant strategic initiatives in Healthcare, 
            IT and Financial Services Information Systems that open up enormous 
            opportunity and potential for the company throughout the region. <br>
            <br>
            The group finally handed over the GSM Billing project to Omantel in 
            September 2004, thus reversing provisions worth RO0.881 million, 
            booked in 2003. This was in addition to TG’s profit contribution of 
            RO0.949 million (2003: RO0.258 million) to the company’s bottomline. 
            Moreover, in October 2004, Imtac bagged the project to implement 
            Real Time Gross Settlement (RTGS) system from the Central Bank of 
            Oman along with the Anti-Money Laundering System implemented for 
            Doha Bank. Imtac recorded a 66 per cent growth in revenue as 
            compared to 2003. Major wins for it included IT projects for Nawras 
            Telecom, Ministry of Tourism, Ministry of Health, Petroleum 
            Development Oman and Sultan Qaboos University.<br>
            <br>
            On the other hand, 2004 was a disappointing year for the company’s 
            Education &amp; Training Group, following on from the successful 
            turnaround achieved in 2003. The phenomenon ended abruptly in Q2 of 
            2004, when effectively government funds for training were expended 
            as a result of the scale of the earlier success. Of the ETG 
            companies, Al Rakaib still performed profitably with impressive 
            results above plan for the year as all its candidates and programmes 
            were already assigned and in place. Both NTI and NHI were, however, 
            caught short. <br>
            <br>
            However, there now seem to be signs of a solution. New government 
            budgets are being assigned and the first quotas of new candidates 
            are being issued to the companies. ‘‘All the elements are in place 
            for a more stable solution going forward,’’ assures Thomas. ‘‘There 
            is dynamic leadership in the Ministry of Manpower, and larger and 
            sufficient budgets are being allocated to sustain a continuous 
            uninterrupted flow of training,’’ he maintains. <br>
            <br>
            The company’s Media Communications Group continued on its 
            profit-making path for the third consecutive year, with revenues 
            climbing above RO3 million and profit just shy of the RO200,000 
            threshold. ‘‘This success is particularly impressive in the Oman 
            market, where communication spends have not grown in real terms for 
            several years,’’ says Thomas. ‘‘The Media Distribution business has 
            effectively converted its doorstep reach into a communication 
            channel, extending its coverage to over 200,000 households 
            delivering print communications across the length and breadth of the 
            country,’’ he declares. <br>
            <br>
            The Publishing and Media Representation business continued to 
            develop its key brands (including Oman Economic Review; Al Mar’a and 
            representation of Khaleej Times). ‘‘MCG also secured expansion into 
            new markets with its Qatar operations launching the English monthly 
            Qatar Today,’’ adds Thomas. In the Interactive business, Global 
            Finance declared BankMuscat (UMS is the development partner for the 
            bank’s Internet solutions) as the Best Consumer Internet Bank 2004. 
            The Advertising business had success in competitive tender, winning 
            assignments for creative design, printing services and international 
            media placement for Muscat Festival 2005; as well as retaining the 
            Oman Air account for a further two years. <br>
            <br>
            <b>Outlook 2005</b><br>
            ‘‘These are exciting times for Renaissance as our people lead the 
            way in delivering an enhanced customer experience that we believe 
            will continue to deliver superior returns for our shareholders,’’ 
            asserts Thomas. The sceptics’ fears of sustaining the excellent 
            performance of 2003 were adequately put to rest by the company’s 
            exceptional 2004 results. However, cautions Thomas, ‘‘The challenge 
            for 2005 is to prove that results such as 2003 are sustainable as a 
            minimum, and exceptional results such as 2004 are increasingly 
            attainable as the company opens itself up to wider opportunity.’’<br>
            <br>
            Nevertheless, as Thomas puts it, ‘‘It is increasingly clear that 
            Renaissance is capable of delivering high earnings on a sustainable 
            basis. There is abundant potential for organic growth of the 
            existing line operating companies as well as scope for geographic 
            expansion. The parent company is well placed to take advantage of 
            any strategic or structural opportunity that may arise through 
            merger, acquisition or market consolidation. Opportunities abound 
            and one thing is clear: when Renaissance comes face to face with 
            opportunity, great things can happen.’’ Touché!<br>
