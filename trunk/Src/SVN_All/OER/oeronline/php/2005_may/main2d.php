<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">TKM 
celebrates 1st birthday</font><font face="Verdana" size="1"><br>
<br>
<b>OER gets face2face with Abdullah Al Jufaili, Head, The Knowledge Mine, the 
business incubator based at Knowledge Oasis Muscat (KOM). TKM celebrated its 
first birthday on April 3. Here’s what Al Jufaili had to say about the program 
and the types of firms setting-up in TKM.</b><br>
<br>
<b>
<img border="0" src="main2d.jpg" align="left" hspace="4" width="120" height="114">What's 
TKM all about – what’s your mission and what do you do? </b><br>
TKM is Oman’s first fully-fledged incubator program. Its mission is very simple 
– to assist start-up and early stage companies grow and prosper. What we do is 
to provide a top-flight facility and services to help firms succeed. We have 
designed the facility for technology-based and knowledge-driven businesses. The 
facility has 29 individual offices ranging from 9 to 45 square metres. These 
offices can be rented individually or in blocks, depending on the space needs of 
a company. TKM is open 24/7 and has a sophisticated telecommunications system. 
Each office has multiple telephone and data points. Firms have access to meeting 
rooms and a boardroom. We have gated parking, a key-pass security system and, as 
is typical with incubators, low-cost office space.<br>
<br>
TKM offers business-consulting services through an on-site mentor program where 
mentoring is provided by organisations such as KPMG, Ernst &amp; Young, Intilaaqah 
and Trowers &amp; Hamlins. In brief, our philosophy is to provide a place where 
firms can concentrate on their core business while we take care of the side 
issues that often distract start-ups – conference rooms, space planning, 
Internet connection, parking, etc. <br>
<br>
<b>Who are your clients, and how are they using your services? </b><br>
We have six start-ups at the moment with five more at the interview stage. Our 
tenants have concentrations from software development to research and 
development for security systems. Several firms are developing proprietary 
software for education and logistics. We have firms specialising in turnkey 
measurement and automation systems and products. We also have a firm 
specialising in multimedia and its applications to third generation mobile 
systems. Many of our tenants have science, maths and engineering backgrounds and 
this has influenced the type of firms they’ve set-up. Indeed, TKM tenants have 
run projects with some of Oman’s major blue chips and feedback has been 
extremely encouraging. <br>
<br>
<b>At what stage do people join TKM? </b><br>
Because admission is competitive and requires a full business plan and profile 
of a firm’s management structure, TKM firms have typically had a fairly solid 
sense of their technology, marketing and operational plans. These turn out to be 
more important to us than the number of people or the source of financing – 
although an indication of financial vitality (bearing in mind the start-up 
status) is important. <br>
<br>
<b>How long do firms typically stay in TKM and at what point in a company’s 
development do they move up and out?</b><br>
We don’t have a fixed graduation date. We anticipate the development time to be 
two to three years, but that cycle seems to be lengthening. <br>
<br>
<b>When interviewing potential TKM tenants, what do you look for?</b><br>
We look for a number of things. For example, are they capable of being number 
one in that category in 10 years? Are they fundamentally changing an industry or 
an experience that is, today, broken or seriously flawed? Are they focused on 
demand creation or demand fulfilment and are they in a space that has defendable 
margins?<br>
<br>
<b>What strengths and weaknesses do you see in Oman’s growing technology base 
and what do you think is important for companies here to succeed? </b><br>
In my opinion, business is increasingly less about technology and more about 
changing and improving traditional ways of doing business. It’s about seeing the 
future and integrating the Internet into every facet of your business. The 
Internet shouldn’t be an initiative in your organisation; it should be a guiding 
principle. In that vein, TKM has the kind of creative business minds that can 
wrap their heads around the changes that need to happen from top to bottom in an 
organisation. <br>
<br>
<b>What have you learnt over the past 12 months that you think helps make a 
start-up work?</b><br>
Execution and focus. Great ideas only take you so far. At the end of the day, it 
comes down to what you’re able to accomplish and how quickly you accomplish it. 
