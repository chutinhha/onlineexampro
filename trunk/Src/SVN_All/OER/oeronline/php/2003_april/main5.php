            <p><b><font face="Verdana" size="1">
            <img border="0" src="car--review.jpg" align="left" hspace="3" vspace="3" width="250" height="186">The 
            Lexus RX 330 (2004 model) makes its local debut. Fasten your 
            seatbelts for an exhilarating ride</font></b></p>
<p><font face="Verdana" size="1">The Lexus RX330, manufactured by Toyota 
Motor Corporation, has been launched to replace the popular Lexus RX300, which 
was first unveiled in Japan and the US in 1998. Rather than changing direction, 
the carmaker has opted to produce a model that should generate more interest 
locally and drive sales into the fast lane. The vehicle has a more powerful 
engine (a definite advantage in the Sultanate), it is a slightly larger vehicle, 
and it is more comfy than its luxurious predecessor. Added to this, there are 
plenty of surprises that should ensure that those who are young-at-heart would 
enjoy taking the RX330 on fun family outings.<br>
While Oman's local car dealership is describing it as an advanced luxury 4WD, 
some auto experts are using terms like hybrid, crossover and SUV (sports utility 
vehicle). Whatever the categorisation, there is no problem understanding that 
the redesign of this particular Lexus has improved its ability to compete with 
rival brands. After all, Lexus is the No. 1 premium SUV in the US. The appeal of 
Lexus in the GCC has also been high with locals associating the brand with 
luxury and value-for-money which, in turn, has tended to lead to slightly lower 
depreciation rates than other luxury brands and better resale value after four 
years or more. Prices for the new RX330 begin from RO14,000 (US$36,400). <br>
The Lexus RX330 will be released in three model types: The Premium (base model), 
the Exclusive, and the Exclusive Sport. OER took the top-of-the-range Lexus 
RX330 Exclusive Sport for a road test in Muscat.<br>
<br>
<b>Outdoors</b> <br>
The RX330 is fresh and sporty particularly when you look at its integrated rear 
spoiler as well as its new-sloped roofline with coupe-like window profile and 
raked tailgate, the car's biggest external departure from its predecessor. 
Another welcome addition to its styling is the introduction of 18-inch alloy 
tyres (these have been upsized from 16 inches) which are chrome-finished on the 
Exclusive Sport which also gets a roof rail and rack and auto rain-sensing 
wipers. Keyless entry and fog lamps are standard features on the base model as 
well as an xenon lighting system that swivels the headlamps which help in better 
illumination of the road around corners. <br>
To ensure that you can open and close the boot with less hassle when you've got 
your hands full of weekly shopping, the most unique characteristic on the RX330 
Exclusive Sport is its power-operated hatch with jam protection. It's quite a 
sight and no doubt a useful sales tool for car salesmen as they demonstrate the 
car's virtues. <br>
<br>
<b>Indoors </b><br>
Inside, Lexus has provided better features and levels of comfort than that 
offered in the RX300 and other vehicles in its class. The RX330 base model has 
dual-zone automatic climate control, a power tilt/telescoping steering wheel 
with memory, and an eight-way power front driver seat with memory (four-way for 
the passenger seat). In fact, there is slightly more space especially for front 
occupants and the front chairs are certainly comfortable while the rear offers 
plenty of headroom and legroom as well as AC ducts.<br>
The spare tyre hangs outside beneath the cargo floor, freeing up room for under 
deck stowage. A latching system automatically reels in the cargo cover when the 
rear hatch is opened. Maximum cargo volume is nearly 85 cubic feet although with 
the rear seats in place and the sleek rear profile of the RX330, this is reduced 
to 38.3 cubic feet. Nevertheless, occupant space remains generous, albeit only 
for five people as Lexus has resisted the urge to pack in a third seat into the 
vehicle. Local Lexus shoppers looking for a seven-seat SUV are urged to consider 
the GX470 instead. <br>
The three-spoke steering wheel (genuine wood in the Exclusive Sport) comes with 
one-touch, easy-to-use controls for the audio system and trip computer. There's 
a sliding centre console, adjustable light in the visor vanity mirrors and a 
Mark Levinson 11-speaker system with an in-dash 6-disc CD changer. <br>
Leather upholstery is fitted in the Exclusive and Exclusive Sport models along 
with a tilt and slide automatic sky window.<br>
<br>
<b>Performance</b> <br>
The RX330 provides reasonable off-road ground clearance and full-time four-wheel 
drive capability. The main reason for the name change is that the V-6 engine has 
been given more muscle, moving up to 3.3 litres from 3.0 litres. This delivers 
an additional 10 horsepower and 20 pound-feet of torque from the DOHC 24-valve 
aluminium block and heads engine which also boasts Variable Valve Timing - 
Intelligent (VVT-i) and an electronic drive-by-wire throttle. Lexus claims that 
252 horsepower at 5,600 rpm makes the RX330 quicker than its major competitors 
that include the Acura MDX, the BMW X5 3.0i, and the Mercedes-Benz ML320. 
Indeed, despite its weight, the all-wheel-drive RX330 with automatic 
transmission has an awesome acceleration from zero to 60 mph in 7.8 seconds. <br>
Fuel efficiency has also been enhanced, according to reports. The estimated fuel 
mileage of the RX330 is 18 mile per gallon (mpg) in the city and 24 mpg on the 
highway. That's one extra mpg gained on each when compared to the RX 300. <br>
<br>
<b>Safety </b><br>
All RX 330 models come equipped with a Vehicle Stability Control (VSC) system 
which controls loss of lateral traction while cornering. VSC and the Brake 
Assist work with the standard four-wheel Anti-lock Brake System (ABS). A tyre 
pressure monitor alerts the driver with a light on the instrument panel in the 
event of tyre under-inflation. The Exclusive Sports model also comes with a 
sonar-guided parking assistance system for front and rear manoeuvres to further 
reduce the risk of accidents. <br>
The high-strength cabin and a comprehensive new supplemental restraint system 
are designed to help provide a new level of crash energy management for 
occupants in the RX 330. Supplemental Restraint Systems (SRS) include 
multi-stage front airbags for the driver and passenger, side-curtain airbags, a 
new knee airbag for the driver and a new large side airbag in the front seats 
that covers the torso, abdomen and pelvis. In fact, there are nine airbags in 
total. <br>
<br>
<b>Verdict <br>
</b>Lexus has created a refined design for its SUV with no heavy-duty 4WD 
add-ons, such as side steps, external spare wheel or bull-bar on the front 
bumper. This makes the RX330 more refined to look and highway-rather than 
off-road-worthy; yet, its all-round driving performance from its high-seating 
position is still lively when required. Some may wish for a little more 
sensibility between gears, but the car's response at cruising speed on a stretch 
of carriageway is smooth and it can handle light off-road conditions without any 
major problems. Another asset is the SUV's precise steering and turning circle, 
which aids manoeuvrability in tight corners. All things considered, the RX330 
seems to deliver everything the original did and adds a great deal more. It 
should, therefore, be a bigger hit. Road test courtesy of Saud Bahwan Automotive 
LLC, the exclusive car dealership for Lexus in Oman.</font></p>
