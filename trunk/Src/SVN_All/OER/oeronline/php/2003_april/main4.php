            <font face="Verdana">Where is Oman?</font></font><p><b>
            <font face="Verdana" size="1">
            <img border="0" src="tourism.jpg" align="left" hspace="3" vspace="3" width="250" height="165">OER finds out if Oman is doing enough to 
put itself on the tourist map </font></b></p>
<p><font face="Verdana" size="1">Oman's business community and government 
are in total agreement that the Sultanate has huge tourism potential. Analysts 
and economists too are unanimous that the country is making steady progress in 
diversifying the economy away from oil dependence to sectors such as tourism. 
The government is aiming to raise the contribution the hospitality industry 
makes to the Sultanate's gross domestic product (GDP) to around 3 per cent by 
2020. <br>
But that path is littered with roadblocks. Oman's tourism sector, like the 
global hospitality trade, was already experiencing a downturn due to 9/11. And 
now, there's the war in Iraq. To the relief of many in the industry, the 
government has continued to plough ahead with tourism projects although the 
country is likely to scale down its marketing efforts in the next few months.
<br>
HE Maqbool Ali Sultan, Oman's minister of commerce and industry, says that 
tourism is one of five policy thrust areas being pursued to drive the economy 
towards speedy diversification and create jobs. &quot;Based on studies, our attention 
is focused on eco tourism, adventure tourism, cultural and heritage attractions, 
water sports including diving, coastal resorts and leisure retreat resorts,&quot; he 
says. <br>
A major priority project for the government is the under-construction Barr Al 
Jissah - a fully integrated US$169-million coastal tourist resort with three 
deluxe hotels. Other priority projects include the Al Sawadi Resort (a 
high-class US$308-million coastal resort which will include two golf courses and 
various hotels and villas), the Rass Al Hadd eco-tourism project developing 
Oman's turtle breeding beaches, the Muscat sea-front development and the Mirbat 
Tourist Village in Salalah. <br>
Because it is labour-intensive, the planned growth in hospitality will generate 
considerable employment. Most hotels in Muscat have an Omanisation level of 
around 30 per cent. &quot;The industry has traditionally enticed young staff that are 
sometimes unsure of the career they want to pursue. This has, naturally, created 
a high staff turnover ratio. But when you look at hotels in Oman, it is easier 
to retain staff here than many parts of Europe,&quot; emphasises one hotelier.<br>
<br>
<b>Marketing edge <br>
</b>A marketing initiative has begun to present the Sultanate as a new, clear 
and distinct national brand building recognition under the slogan 'Oman...The 
Essence of Arabia'. One tourism analyst explains that this is definitely not the 
way forward for the country. Indeed, he is adamant that Oman is not Arabia's 
essence, and should be sold as something &quot;uniquely different&quot; that is better 
than elsewhere in the Middle East. The expert explains that, in a few languages, 
the slogan can be misinterpreted. For example, the word 'essence' in French 
means petrol. &quot;Oman is not the petrol of Arabia. It boils down to positioning 
your product correctly and, so far, this hasn't been done,&quot; he adds. <br>
&quot;There has been a jump between what was achieved before and what is being done 
today to promote the country,&quot; says Khalid bin Al Zadjali of Oman's tourism 
marketing department. He mentions that there are advanced plans to introduce new 
brochures and maps (around 20,000 new pocket maps of Muscat in English and 
Arabic are planned for free distribution before the end of the month) while a 
new film distributed to airlines is being used on flights before they land at 
the Sultanate of Oman's Seeb International Airport to inform incoming tourists 
about arrival procedures.<br>
&nbsp;These measures are helpful for tourists who have already entered the 
country. What about those who don't have an idea about where the Sultanate is?
<br>
While the marketing department of the tourism directorate is keen to publicise 
its efforts to promote the country at travel exhibitions, some hoteliers say 
this is simply not enough. Attending events such as the ITB Berlin travel trade 
fair, the world's largest tourism exhibition, are the bare minimum that any 
destination needs do within a broader master plan. The local industry is 
seemingly looking for proof that there is going to be more serious investment on 
tourism, that it will be a coordinated effort across a number of ministries and 
private sector establishments, and that effective messages will be broadcast to 
targeted audiences around the world. <br>
&quot;A typical tourist to Oman spends a few days here, but wishes he spent more time 
in the country. They are usually unaware of how much there is to do. 
Essentially, this boils down to improved destination marketing,&quot; explains a tour 
operator. He says that an easing of visit visas would also help. In February 
2003, the government eased visa regulations to facilitate the inflow of tourists 
during the Muscat Festival. Citizens of 65 countries were eligible to obtain the 
new visit visa on arrival at airport or land border-posts. Now that the festival 
is over, citizens from many of these countries need to apply for a three-week 
visa. <br>
&quot;Tourism is still the biggest growing industry in the world. When there is a 
more stable geo-political situation in the Middle East, Oman is perfectly placed 
to benefit if it succeeds in creating prestigious resorts and attractions such 
as green golf courses,&quot; says a local airline manager. <br>
The region's hotel sector was left reeling for several months after 9/11, 
particularly in tourist destinations like Dubai, where staff were given shorter 
working hours or compulsory leave, and occupancies at prominent properties 
dropped to all-time lows. There is a fear that the war in Iraq may bring the 
same again, or worse.<br>
Now may not be the time to spend the money on destination marketing, but looking 
beyond the war in Iraq, Oman will need to do even more to improve the perception 
of where it is and what it has to offer. </font></p>
