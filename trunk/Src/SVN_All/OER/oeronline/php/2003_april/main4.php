that, recent developments in the region notwithstanding, Oman's tourism strategy 
is on the right track </b></font>
<p><font face="Verdana" size="1">Oman's tourism sector contributes around 
0.3 per cent to the Sultanate's GDP (gross domestic product). We expect that our 
tourism strategy and the efforts from our marketing programme can help push this 
contribution up to 3 per cent by 2020.<br>
International Development Ireland Ltd (IDI), a management support team that the 
government has engaged to develop the tourism sector here, will help keep our 
strategy on course. IDI has undertaken detailed research work in the past 
(including the Tourism Priority Action Plan) and, based on these studies, has 
formulated objectives to make the Sultanate of Oman compete internationally as a 
tourist destination.</font></p>
<p><font face="Verdana" size="1"><b>Research recommendations</b><br>
The government has conducted several studies in the recent past, and some of the 
recommendations have been implemented. More research needs to be undertaken to 
figure out how best to approach tourists. The kind of tourist that other 
countries are seeking to attract are different from those Oman is targeting. 
Research suggests that even first-time tourists visiting the Sultanate are 
familiar with the destination beforehand. They haven't made a spontaneous 
decision, knowing nothing about the country, but have probably read about the 
culture and adventure/leisure opportunities here, and are coming for a specific 
purpose. More often than not, they are aware that Oman has the Al Bustan Palace 
hotel and that it is home to over 150 forts and castles.<br>
The government established Oman Tourism and Hospitality Academy last year to act 
as a source for the workforce necessary to develop our tourism industry. The 
first batch of graduates from the Academy will enter the job market next year.
</font></p>
<p><font face="Verdana" size="1"><b>Marketing momentum <br>
</b>We are planning a new tourism advertising campaign for Oman in the next few 
months. The country will host a French TV crew soon, and we have plans to 
increase the number of representatives in countries around the world. At 
present, we have five representatives: one each in France, Japan, Australia, 
Holland, and the UAE. We are now reappointing a representative in Italy. By May 
2003, we will appoint one in the UK, and before the year-end, we hope to have a 
representative in Germany too. Appointing the representatives has helped us get 
a deeper understanding of the target markets for tourists. It has enabled us to 
market directly to these countries and helped our tour operators and hoteliers 
communicate to their counterparts more effectively.<br>
While we are confident that the local tourism sector will progress with the help 
of IDI, there are several uncertainties at the moment. The geopolitical 
situation in the region is perceived to be unstable. For the time being, there 
are fewer people travelling anywhere. Till the time the war in Iraq continues, 
certain travel agencies may consider removing Oman from their brochures. We've 
been meeting them to prevent this from happening, and reinforcing the fact that 
Oman is a safe and friendly tourist destination. Last month, at ITB Berlin, we 
met with many tour operators in this regard. </font></p>
<p><font face="Verdana" size="1"><b><font color="#FF0000">'Oman needs a national policy on 
tourism' </font> </b><br>
<b>Nabeel Jawad Sultan, Deputy Managing Director, Jawad Sultan Enterprises, claims 
that there is more to Oman's tourism woes than the unstable geopolitical 
environment </b> </font></p>
<p><font face="Verdana" size="1">While a lot has happened in the tourism 
industry worldwide for the past decade or so, Oman has done little in terms of 
attracting considerable amount of tourists. This is despite our commitment to 
develop the tourism sector being strong. Many untiring efforts, including the 
introduction of simplified procedures for visa, couldn't produce the desired 
results. So, where does the problem lie? Is it the aftermath of 9/11 and new 
global realities, or is it something beyond that? Well, part of the answer lies 
in the changes that took place post 9/11 but, certainly, there is more to it 
than just the political environment. <br>
For more than ten years now, the Sultanate has lacked a consistent national 
policy on tourism. Therefore, we need a policy that will coordinate and 
integrate the overall efforts so as to develop a focused tourism industry in the 
country. Our promotional efforts need to take a 360-degree turn in terms of - 
What we are marketing? Whom are we marketing it to? And where are we marketing 
it?</font></p>
<p><font face="Verdana" size="1"><b>Focused tourism <br>
</b>The principle of focused tourism is not a new one. It involves deploying, 
investing and utilising the available resources to areas of potential attraction 
for tourists. It also involves marketing those attractions directly to the 
targeted tourists rather than nations, and is thus more individual-centric. 
Basically, the concept means that, regardless of a country's numerous 
attractions, certain aspects should be considered the main selling points for 
luring the tourists. <br>
The fact that Oman accommodates the desert environment, mountains, rivers and 
beaches within its topography makes it a unique place to be seen and 
experienced. The activities associated with these - exploring the deserts, 
mountain hiking, driving in rugged environments, scuba diving, snorkelling, 
fishing, swimming, and many other speedboat-related activities - make Oman a 
major attraction. <br>
So what does &quot;focused tourism&quot; mean to Oman? I think our country has all the 
elements of a focused-type of tourism in the areas of nature and adventurous 
activities. Our primary aim should be to market the core attractions (i.e., 
adventure and nature). This will require considerable investments in upgrading 
the infrastructure of the tourism industry, and would be necessary to bring it 
in line with our national strategy.<br>
<br>
<b>The strategic alliance<br>
</b>One of the major 
factors that could benefit us from tourism point of view is to market Oman as an 
extension to Dubai experience and vice-versa. The idea that a tour to Oman from 
Dubai is just a part of the overall Dubai experience gives us instant access to 
a considerable market with minimum effort. Dubai, perhaps the entire UAE, and 
Oman complement each other. Hence this opportunity should be utilised 
effectively. Lastly, the development of tourism in Oman requires more systematic 
approach, which should involve all the stakeholders. The national policy on 
tourism should be laid out and communicated properly to all the stakeholders. A 
marketing plan needs to be created which reflects our strategy and is consistent 
with what is being offered to the tourists.<br>
