&quot;Not everyone is destined to become a CEO or board chairman. Looking back at 40 
years in the industry, I remember a large number of professionals who have been 
promoted as a good manager into a leadership role and they don't have one ounce 
of command posture and they fail themselves, and sometimes their organisations, 
as a result,&quot; he says. </font></p>
