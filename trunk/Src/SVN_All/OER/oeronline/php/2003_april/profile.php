                  </font></b><font face="Verdana" size="1">Jack Helton has enjoyed a fascinating 
career. While working with the best in business in the US, Russia and Oman, he 
has rubbed shoulders with leaders such as former US president George W. Bush 
senior, former Soviet leader Mikhail Gorbachev, Georgian president Edward 
Shevarnadze and His Majesty Sultan Qaboos.<br>
Today, he works as CEO of Salalah Port Services, one of the Sultanate's largest 
blue chip companies that began operations in November 1998. The company has 
posted a net profit in three of the past four years while, in 2003, it expects 
to pay its shareholders a dividend on their investment posting net earnings of 
RO 2,425,411 last year.<br>
Helton has a talent for making things happen. He began his career with Sealand 
in 1977. In 1997, the company persuaded him to put his retirement plans in 
deep-freeze and embark on a mission to help Oman's government develop the port 
in its second-largest city as one of the leading trans-shipment hubs in the 
world. It is an opportunity that he has relished, and any concerns he might have 
had soon melted away. <br>
Prior to this assignment, he spent four years as the vice-president of Sealand 
in the former Soviet Union. &quot;I had a desire to go there ever since I studied 
political science at the Texas Tech University in Lubbock, Texas. I was 
intrigued about the ideology behind communism and to learn what made the 
Russians tick.&quot;<br>
Before moving to Moscow, he was the VP of sales for Sealand's US Atlantic 
Division, was appointed VP of the North American operations and earlier VP in 
charge of the Alaska division. <br>
Life has been far from plain sailing for Helton, who is at present working on 
his autobiography. In his book, he will discuss some deeply unnerving times he 
earlier experienced during 17 years of work in the US trucking industry prior to 
his career at Sealand. Offering an example, he reflects upon the daunting task 
entrusted upon him of dealing with a difficult trade union situation in St 
Louis. At that time, trade unions were so powerful that they controlled the way 
transport firms operated. As the company attempted to regain authority via 
compromise rather than confrontation, an ugly dispute ensued. <br>
&quot;I believed it could be settled amicably through negotiation. But the strikes 
turned nasty and some drivers were killed for walking beyond the union's picket 
lines. My family faced harassment. There was even a contract out on my life. For 
personal security, I carried a shotgun with me at all times for several months. 
In the end, common sense prevailed. The unions lost the conflict and were made 
bankrupt. Indeed, it was the first time in US history whereupon a union was 
ordered to pay damages to a group of companies following industrial action,&quot; he 
recalls. <br>
Anything is possible; everything is negotiable, he notes. &quot;Weakness shines like 
a beacon. If you feel you have no options, you are seen as vulnerable. In such 
situations, you've got to be convincing. You need to have patience and 
perseverance or you're going to lose.&quot; <br>
Helton's robust tone of voice is occasionally mingled with his Texan turn of 
phrase and his honest facial expressions. In conversation, you can catch his 
short glimpses of unspoken goodwill, a gracious smile or an approving nod.<br>
                    <br>
                    <b>Who am I? </b><br>
Helton appears to project a natural aura of self-confidence that could be 
mistaken for simple arrogance by the less informed. &quot;There's a fine line between 
confidence and cockiness. I have a passion for the things I do, but this 
enthusiasm is sometimes misconstrued,&quot; he explains. <br>
Scratch the surface, and you find Helton is an intellectual with a great deal of 
charm and pragmatism. Throughout the meeting, he ruthlessly asks a question 
which was a guiding principal passed on to him by his 94-year old father: &quot;Who 
am I?'. This, in turn, produces a refreshing independence of mind and an 
analytical discussion on a broad range of subjects as the question &quot;Who am I?' 
can be applied to organisations and nations as well as people.<br>
&quot;I'm just another player on the team; I happen to be the coach, but I'm a player 
nonetheless.&quot; Helton believes adjusting your personality quickly and effectively 
is a critical point whether you're meeting with blue-collar workers or royalty.<br>
&quot;I have the ability to be logical and reasonable. I try to absorb a lot of 
information before I take a decision. I'm a Libran; so, it always takes me time 
to do so,&quot; he explains. If you believe in the zodiac as Helton does, Libra is 
the sign of harmony and justice. Librans are meant to be emotionally rich, 
gifted with tact and delicacy, and skilled at making and keeping friends. To 
sceptics of the zodiac, Helton actually has many qualities that closely match 
the interstellar interpretation. He certainly invites confidence and trust among 
his staff and colleagues. <br>
But the scales have two sides, and Librans are apparently beset by the tug of 
opposites and slow deliberations. &quot;Sometimes, people get frustrated with me 
because I don't take a quick decision. But if a decision doesn't feel right, my 
advice will be to sleep on it.&quot;<br>
&quot;When making critical decisions, if people are pushy for a speedy response, I 
have been known to tell them that if they want an answer today, they'll get one 
they probably won't like.&quot; <br>
                    <br>
                    <b>Favourite sayings </b><br>
Helton has put to memory several golden nuggets of wisdom throughout his career 
that come from experience rather than management textbooks. One of his favourite 
sayings is, &quot;If you're not prepared for the answer, don't ask the question'. 
This is an important principal in business that has been reinforced by the 
tightening up of corporate governance issues around the world, he claims. <br>
The thought-provoking principals he's picked up through life have seemingly 
helped Helton to take tough personal and business decisions and develop 
solutions to complex problems. But he warns of information overload too. <br>
&quot;I read an article which put this succinctly. It's important to discern what you 
need to remember and what you need to trash.&quot; <br>
These days, the sphere of understanding is so great and education plays a bigger 
role in providing youth with the skills to cope with this. Young people need to 
grasp every ounce of knowledge they can. They also need worry about the 
disinformation they receive that can cloud judgements and reduce clarity of 
thought. <br>
&quot;We must prepare young Omanis to learn beyond the sphere of their own country 
because they will have to compete in an international environment,&quot; he 
concludes. <br>
                    <br>
                    <b>A born leader </b><br>
Helton is imaginative, reliable and, has the ability to motivate and, above all, 
lead. Indeed, leadership is probably his strongest asset. &quot;I is what I is,&quot; he 
smiles. You can fine-tune people skills, but leadership is not learned. You're 
either born a leader or not.&quot;<br>
During the 1980s, the chairman of Sealand afforded Helton the opportunity to go 
on courses to improve his career prospects. Without people like him along the 
way, the path to success would have been harder for him. &quot;He was a Harvard grad, 
and I was a Texas bumpkin that used colloquialisms,&quot; confesses Helton, &quot;but he 
saw some potential in me and helped me to attend college and small group 
seminars on business etiquette and social graces.&quot; &quot;One has to be the best you 
can be, but the best will never be,&quot; he emphasises. <br>
                    <br>
