        <font color="#CC3300" face="Verdana" size="1">&nbsp;</font><font color="#CC3300" size="2" face="Verdana"><br>
        An oasis in the desert of Muscat</font></b><font size="1"><br>
        <font face="Verdana">The Royal Botanic Garden Edinburgh (RBGE), Scotland, is to create a 
        botanic garden in Muscat, one of the largest of its kinds in the Arabian 
        Desert. With 425 hectares in area, the park’s size will be four times 
        that of the Royal Botanic Gardens in Kew, UK. The botanists will be 
        cataloguing 1,200 species.<br>
        <br>
        Says Sabina Knees of RBGE: “The flora is growing in very stressful 
        conditions, in temperatures of up to 500 C. They are species living 
        right at the upper limit of existence. Its a great challenge.” The work 
        on the garden is at a schematic stage and is due to be completed in 
        2010.<br>
