        display apartments</b></font><font size="1"><br>
        <img border="0" src="bus4.jpg" width="250" height="168" align="left" hspace="6" vspace="4"><font face="Verdana">The US$2 billion integrated development project The Wave, Muscat has 
        unveiled its display apartments. Located within the fully integrated 
        residential community, the display apartments showcase all that The Wave 
