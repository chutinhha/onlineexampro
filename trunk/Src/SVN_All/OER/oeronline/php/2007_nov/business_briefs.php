</font><font color="#CC3300" size="2" face="Verdana"><b>Oman Refinery Company 
arranges $1.37bn</b></font><font size="1"><br>
<img border="0" src="bus1.jpg" width="250" height="154" align="right" hspace="8" vspace="4"><font face="Verdana">Oman Refinery Company (ORC) has signed a US$1.37-billion financing agreement 
with local, regional and international banks. The amount will be used mainly to 
prepay existing debt facilities at Sohar Refinery Company (SRC) and ORC ahead of 
the merger of the two companies. The government has decided to merge SRC with 
ORC to create a world-class refining company. The merger will be implemented by 
the absorption of SRC by ORC, which will be renamed Oman Refineries and 
Petrochemicals Company (ORPC). Following the merger, ORPC will be owned 75 per 
cent by the government through the Ministry of Finance, and 25 per cent by Oman 
Oil Company. The move seeks to combine Oman’s two refineries – ORC in Mina Al 
Fahal (Muscat) and SRC in Sohar, which are connected by a 250-km pipeline.</font><br>
