        </font><font color="#CC3300" size="2" face="Verdana"><b>GIH acquires 
        majority stake in Al Jazeera Steel</b></font><font size="1"><br>
        <font face="Verdana">Kuwait-based Global Investment House’s Global Buyout Fund has concluded 
        its first investment in Al Jazeera Steel Product Company, an Omani 
        company listed on the Muscat Securities Market. Global Buyout Fund, the 
        recently launched buyout fund focusing on investing in the GCC and MENA 
        region, subscribed to 63,697,960 equity shares in Al Jazeera at 0.325 
        baisas which is at a 43 per cent discount to the market value (market 
        price 465 baisas as of October 21) resulting in the Fund taking a 
        majority stake of 51 per cent post the capital infusion. Al Jazeera 
        Steel, established in 1997, is a market leader engaged in the 
        manufacturing and distribution of mild steel black and galvanised 
        round/square tubes/pipes. The company is currently in the final stages 
        of implementing a new merchant bar mill to offer a broader range of 
        products that would cater to the construction sector.</font><br>
