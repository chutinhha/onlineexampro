        three bedroom luxury apartments are set in buildings, which are 
        traditionally styled. The interiors of the apartments have been done up 
        by interior designer Mary Clephane. The apartments have been designed 
        with lots of glass, shaded with partitions and Macrabia (Omani 
        traditional shades) set around their balconies. Modern, spacious, light, 
        and practical, the apartments have been designed to be easy and fun to 
        live in, upholding a relaxed way of life that is enhanced by an open 
        living/ dining/ kitchen layout. Modern stylish kitchens, which are also 
        practical and hardwearing, have been fully integrated with high quality 
        electrical appliances to make life that much easier for the owners. The 
        one-bedroom apartment has a harbour feel, very light, sunny bright, 
        crisp cottons – it’s all about the beach and the sea. The two-bedroom 
        apartment is more contemporary with a deep red shiny kitchen, a black 
        4-poster bed and deep-red leather sofas all on a sand-coloured tiled 
        background. The three-bedroom apartment is more ‘family’ oriented with 
        bags of space inside and outside for the whole family.</font><br>
