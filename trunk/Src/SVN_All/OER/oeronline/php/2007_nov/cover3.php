<font face="Verdana" size="4">Appreciate Rial<br></font></b>
<font face="Verdana" size="1"><b>A moderate appreciation may prove to be a win-win solution, preventing the 
Omani Rial from losing its purchasing power, says Munir A Makki, Managing 
Director &amp; President, FINCORP</b><br><br>
<img border="0" src="cover5.jpg" align="left" hspace="6" vspace="4">Currency valuations are a function of a number of macro and micro-economic 
factors. Political consideration does play a major role. There are advantages 
and disadvantages of either linking or de-linking the Omani Rial from the US 
Dollar or actually appreciating the currency. Our foreign currency reserves are 
in US Dollars and all our oil exports are in US Dollars. Thus, in my view, it is 
best to peg the Omani Rial to the US Dollar, with a reset of the Omani Rial 
exchange ratio to the US Dollar. This is because I think there will be a time if 
the slide in the purchasing power of the US Dollar continues and if corrective 
measures are not taken, it will not reflect the true intrinsic value of the 
Omani Rial. <br><br>The Omani economy is on the upswing, with major investments coming up, specially 
in the infrastructure sector. All this has been fuelled by increasing oil 
prices. Most industries, services and the real estate sectors have benefited 
from the economic boom and are poised for further growth and consolidation. All 
the above factors have contributed to the strengthening of the Omani Rial and it 
may not augur well at this crucial juncture to weaken the currency in view of 
its peg with the US Dollar, which all are aware is going through a lot of 
turbulence. <br><br><b>Weighing the options<br></b>We therefore need to address this issue with a solution that is a win-win 
for all. Maybe we can consider a moderate appreciation – say 2-3 per cent from 
time to time probably every six months or one year or-so that there is some 
adjustment. This is the only way the Omani Rial can be stopped from losing its 
purchasing power. Since most of our foreign reserves, imports and exports are 
denominated in US Dollars, the policy may be that we are neutral and that there 
need not be any adjustment in the real value of the Omani Rial. However, the 
flip side of this policy is that it affects the local currency by way of reduced 
purchasing power and a reduction of the disposal incomes of expatriates in real 
terms. <br><br>Let us look at it on another dimension, if the Omani Rial gains by say 5 per 
cent, it will be beneficial when it comes to making purchases in other 
currencies for buying machinery and services from countries such as Japan, 
Germany, etc. This way, we might be able to bring down the cost of the machinery 
if we deal with other currencies. <br><br>I don’t believe or advise that we should completely delink the US Dollar peg and 
opt instead for a basket of currencies because that would be very complicated. 
It will be very difficult for us to administer because we are still a 
predominantly oil-based economy. Maybe we should appreciate the Omani Rial 
slightly in a phased manner. This will address some of the issues – like helping 
expatriates improve their disposable incomes and enable cheaper foreign goods 
coming from non-dollar based countries. <br><br>Oman had devalued its currency in the 1980s by 11 per cent. This was basically 
to address the big problem of oil prices going down at that time. If the 
government was able to devalue its currency at that time, then we do have strong 
case for appreciation now. I am not saying a drastic 11 per cent but may be a 5 
per cent appreciation. It will really address the issue in a much more practical 
way, while we continue to be pegged to the US dollar.<br><br><b>Kuwait lead<br>
</b>In this context, we can make a comparison to the lead taken by Kuwait in 
addressing the real value of its currency. As all are aware, Kuwait is also a 
predominant oil-based economy. But their investments outside are of a large 
magnitude. They have bought companies in Singapore, Europe and the US. Most 
probably, the income from these investments is equal to the output in oil. 
Kuwait therefore needs to be viewed in a slightly different context and that is 
why they are in a position to contemplate de-linking to the US Dollar and opting 
instead for a basket of currencies. The net effect of this change is that while 
other Gulf countries have seen a dip in the real purchasing power of their 
respective currencies, in the case of Kuwait there has been an appreciation of 
about 3 per cent. <br><br><b>Pegging to the US Dollar</b><br>The US may not view favourably any country that tries to de-link from the 
Dollar. Iraq tried to do so and paid heavily; and now Iran. De-linking of the US 
Dollar is very destabilizing for the US economy. Let us not forget that the 
strength of the US Dollar emanates not only from its huge economy but also from 
the fact that a huge chunk of global trade takes place in that currency. So, if 
the need for the US Dollar were less, why would Singapore or Taiwan or China buy 
US treasury notes? So, it is a political issue as well. Although Venezuela, an 
OPEC member, does not have a too comfortable relationship with the US, it still 
prefers to sell oil in the US Dollar. <br><br>Most of our government assets and reserves are in US dollars. Also, the annual 
budget is based on Dollar-Rial ratios. Now, if we were to alter the US Dollar-Rial 
parity by 5 per cent, then automatically there will be an increase of 5 per cent 
in the budget in order to meet expenditures. I think this in my view is 
manageable. The positives can far outweigh the increases in expenditure by way 
of better management of inflation and also providing necessary boost to the 
economy to ensure that the momentum achieved in economic growth is sustained, if 
not improved.<br><br><b>Forecast<br></b>I personally do not visualise or recommend any major shift in our current 
monetary system of pegging the Omani Rial to the US Dollar. I, however, strongly 
believe that there is good justification instead for an appreciation of 5 per 
cent in the Omani Rial. In the long term we should try to correct the 
devaluation of 11 per cent, but let us do it in phases. I am advocating 
revaluation of the Rial to also restore the parity that existed earlier. </font></p>
