<p align="justify" style="margin: 3px 10px"><font size="1"><br>
</font><font size="2" color="#CC3300"><b>Greenpeace: ‘Toxic materials in iPhone’</b></font><font size="1"><br>
<img border="0" src="gcc16.jpg" width="175" height="152" align="right" hspace="6" vspace="4"><font face="Verdana">Greenpeace International has criticised Apple’s iPhone, saying it includes 
hazardous materials that other mobile phone makers have eliminated. Greenpeace 
has accused Apple of toxic chemicals in its products and post-sales electronics 
recycling in the past as well. Greenpeace claimed that an analysis done on a 
disassembled iPhone by an independent lab in the UK found toxic brominated 
compounds and hazardous PVC (polyvinyl chloride) in multiple components of the 
handsets. Bromine, a chemical used in fire-retardant compounds, was present in 
more than half the 18 samples taken while toxic phthalates made up 1.5 per cent 
of the ear bud cables’ PVC coating. <br>
<br>
“Apple is far from leading the way for a green electronics industry as 
competitors like Nokia already sell mobile phones free of PVC,” said Zeina 
Alhajj, Greenpeace toxics campaigner, in a press statement. The group also 
criticised Apple for not offering a global take-back recycling policy to match 
those of Nokia and Sony Ericsson. <br>
<br>
In its most recent ‘green’ ranking of major consumer electronics and computer 
companies, Nokia and Sony Ericsson topped the list last month at number one and 
number two respectively, with corresponding scores of 8.0 and 7.7 out of a 
possible 10.0. Motorola, with, 6.7, took ninth. Apple, meanwhile, held 12th 
rank, with a score of 5.3. Only one company, Panasonic, scored lower. The lab 
analysis of the iPhone can be found on the Greenpeace website. </font> </font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="right" style="margin: 3px 10px">
