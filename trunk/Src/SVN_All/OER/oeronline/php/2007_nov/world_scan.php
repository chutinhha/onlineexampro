</font><b><font face="Verdana" size="2" color="#CC3333">Airbus finally delivers 
first superjumbo </font></b><font size="1"><br>
<img border="0" src="gcc14.jpg" width="225" height="133" align="right" hspace="6" vspace="4"><font face="Verdana">Airbus has delivered its first finished A380 superjumbo jet to Singapore 
Airlines, 18 months behind schedule. The construction of the A380, able to carry 
up to 800 passengers, has been beset by persistent and costly delays. It has 
also been overshadowed by allegations of insider trading against managers at 
parent company EADS. Singapore Airlines took delivery of the double-decker plane 
at Airbus headquarters in Toulouse. Airbus has stated that it is on track to 
meet its target for A380 deliveries in 2008 and do not envisage further delays. 
To meet its revised production schedule, Airbus must hand over 13 completed 
planes in 2008 and four every month in 2009 and 2010. Delays to the flagship 
project have cost Airbus US$8.5 billion and analysts believe the planemaker must 
now sell 420 models to make a profit on it. It currently has 189 confirmed 
orders and tentative sales agreements in place.<br>
