<p align="justify" style="margin: 3px 10px"><font size="2" color="#CC3300"><b>
Global FDI nears $1,500bn record <br>
</b></font><font size="1"><font face="Verdana">“Global inflows of foreign direct investment (FDI) are 
on track this year to surpass the record US$1,411 billion seen in 2000 despite 
the turmoil in financial markets, the UN Conference on Trade and Development (UNCTAD) 
said. The Geneva-based agency’s annual World Investment Report showed that FDI 
inflows amounted to US$1,306 billion in 2006 – the highest since 2000. UNCTAD’s 
secretary-general Supacha Panitchpakdi, however, said that financial instability 
and high energy prices made the forecasts uncertain.<br>
<br>
The US has regained its position as the favourite destination for foreign 
investors… but developing and former communist countries had their fastest 
growth in investment last year, driven by cross-border mergers and acquisitions. 
Investment flows into developing countries rose 21 per cent from 2005 to US$379 
billion in 2006.<br>
<br>
At US$36 billion last year, foreign direct investment in Africa was double its 
2004 level because of improved prospects for corporate profits and a more 
favourable business climate. But Africa’s share in global foreign direct 
investment dropped to 2.7 per cent from 3.1 per cent in 2005. The largest 
inflows to developing economies were to China, with US$69 billion. Among 
transition economies, the largest inflows were to the Russian Federation, with 
US$29 billion.</font><br>
