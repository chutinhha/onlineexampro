<p align="justify" style="margin: 3px 10px"><font size="2" color="#CC3300"><b>
Google health platform in 2008 <br>
</b></font><font size="1">
<img border="0" src="gcc15.jpg" width="200" height="129" align="left" hspace="6" vspace="4"><font face="Verdana">Google plans to bring its immense data storage and 
organisation capacities to the field of medical care and patient records, 
Marissa Mayer, the company’s head of search, said at the Web 2.0 Summit. Telling 
her audience to “expect a lot of activity in the coming months,” Mayer said that 
the long-expected Google Health initiative will formally appear in early 2008.
<br>
<br>
“The health care industry generates a huge amount of information every year. 
It’s a natural core competency for us, to understand how to organise all that 
data,” she said. As in other areas of its business, Google faces a formidable 
competitor in the race to bring the resources of the Internet to personalised 
health care in the form of Microsoft. <br>
<br>
Earlier this year, Microsoft acquired Medstory, a startup specialising in search 
software optimised for finding health information. Microsoft has not publicly 
disclosed its plans for a health-related product, but is said to be working on 
an offering that combines software with an online component. <br>
<br>
Google has developed a prototype online platform for its health offering that 
incorporates personal medical records, health care-related search features, diet 
and exercise regimens, a localized “find a doctor” application, and other 
elements, Mayer confirmed. The company has shown the prototype to unspecified 
partners and is having both Google employees and “trusted testers” beta-test the 
system.</font><br>
