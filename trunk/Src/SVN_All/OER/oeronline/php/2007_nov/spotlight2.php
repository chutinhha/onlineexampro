<font face="Verdana" size="1">On April 2 this year, R&amp;SA Middle East won the ‘General Insurer of the Year 
2006’ award at the Middle East Insurance awards held in Dubai. The prestigious 
event, the only region-wide competition recognising excellence in all parts of 
the insurance industry, was sponsored by Policy Magazine, the Middle East 
equivalent of Post Magazine. R&amp;SA is the first foreign Insurer to win this 
award. The ‘General Insurer of the Year’ was one of the awards that recognised 
insurance industry initiatives and efforts in areas such as marketing, 
innovation, training, product development, corporate social responsibility and 
e-business.<br>
<br>
Winning the award showcased how Royal &amp; SunAlliance made significant 
contributions to the Middle East insurance industry in areas such as the 
localisation efforts in recruitment, the drive towards an excellent corporate 
governance framework, industry leading innovations such as ‘One Stop Cargo’ and 
Pet Insurance, best practices around customer management and the importance 
given by the company to risk and claims management – two critical areas of the 
insurance business. <br>
<br>
Sanjeev Jha, Managing Director, Royal &amp; SunAlliance, Oman commented: “R&amp;SA’s 
winning submission showed successful management of change and a proven ability 
to meet the demands of the market, regulators and government. ” The General 
Insurer title was one of two awards won by R&amp;SA since the inception of the 
Middle East Insurance Awards, the first one being the e-business award for R&amp;SA’s 
Middle East Broker Interface.</font><p align="justify" style="margin: 3px 10px">
