<font face="Verdana" size="1">Commenting on the competitive landscape, Woodward adds, “As new players continue 
to enter Oman’s relatively small market, the intense competition already present 
will continue unabated. That said, it will be particularly tough for the new 
entrants, unless they can demonstrate a real differentiation on something other 
than price. The market has seen many new entrants over the years, but the 
established leading companies have well developed brands and will not surrender 
their customers easily.”<br>
<br>
Details Khadr, “The economy has shown consistent stable growth. This naturally 
has positively impact on the insurance premium, which has also increased. But we 
need to avoid saturation as this will inevitably imply reduced prices; lower S&amp;P 
rated re-insurers and less comprehensive cover.”<br>
<br>
Kamath comes up with following response, “Whilst competition is healthy and good 
to the insuring public, too many players in a small market could lead to 
deterioration in service and products. Insurers vying for market share without 
sound underwriting will suffer and this will directly affect shareholders and 
customers. While we understand the reasons for allowing new players in the 
market, there is just not enough business for all to survive profitably.”<br>
<br>
Another area of concern for the industry is the low penetration of reinsurance 
companies – and, as a result, local companies remain dependent on foreign 
re-insurers to cover large risks. Explains Kamath, “Large projects and 
properties are mostly reinsured outside with very little sharing in the local 
market – this does not help in making local insurance companies stronger and 
more self-reliant.”<br>
<br>
On the whole, Oman’s insurance industry is going through a transition phase 
where players are booking vigorous profits and their reach, is facilitated by 
the best global regulatory practices, even as a balance needs to be achieved 
between the premium income and the quality of products and services offered.</font><div align="right">
