<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Insuring the future</font></b><font face="Verdana" size="1"><br>
<b>The insurance industry in Oman is set to boom, given the unprecedented growth 
of the economy. However, too much action by players on a small turf has its own 
dangers, cautions Sunil Kumar Singh as he analyses the prospects of the industry 
in the Sultanate</b><br>
<br>
Oman’s insurance sector is on its way up; buoyed by a positive regulatory 
structure, higher asset quality, better performing companies and a healthy 
macroeconomic bottomline. More and more international as well as national 
players are entering in the market, given the feel-good factor sweeping the 
economy and the growing need for insurance products due to an increasing 
population. <br>
<br>
The industry looks set to have a very promising future and if an estimate by 
Business Monitor International, the London-based think-tank on country risk, 
industry intelligence and company research, is to be believed, the total 
non-life premium income of insurance companies in Oman is forecast to reach 
RO153 million in 2010 from RO92 million in 2004; total life premium is set to 
grow to RO82 million in 2010 from RO15 million in 2004; and total premium is 
expected to reach RO235 million from RO107 million in 2004. <br>
<br>
According to Capital Market Authority (CMA), the industry regulator, the 
insurance sector posted strong growth last year as the volume of premium jumped 
by 16 per cent to touch RO144 million compared to RO117 million in 2005. 
Statistics further reveal that the number of insurance policies was up 5.8 per 
cent from 648,603 policies last year. According to CMA, the number of insurance 
players has also increased to 17, with 10 national companies – Al Ahlia 
Insurance Company; Dhofar Insurance Company; Falcon Insurance Company; Muscat 
Insurance Company; Muscat Life Insurance Company; National Life Insurance 
Company; Oman and Qatar Insurance Company; Oman United Insurance Company; Royal 
&amp; Sun Alliance Insurance; and Al Madina Gulf Insurance Company–and seven foreign 
insurance companies – Al Nisr Insurance Company; American Life Insurance 
Company; Arabia Insurance Company; Axa Insurance Gulf; Iranian Insurance 
Company; Life Insurance Corporation (International); and New India Assurance 
Company Ltd. The total investment in the sector grew by 31 per cent to RO179 
million, according to CMA’s latest Insurance Market Review.<br>
<br>
Market players are upbeat on the sector’s growth prospects. As Deepak Kamath, 
Area Manager, Oman, AXA Insurance (Gulf) says, “Insurance is linked to the 
national economy and a robust economy means a higher need for insurance. We 
predict that the growth rate this year for the insurance sector will be at least 
20 per cent and this level would be sustained over the next two years.” Adds 
Khalil Ahmed Al-Harthy, Asst. GM, Al Ahlia Insurance Co, “The insurance market’s 
non-life premium has witnessed an average growth of 19 per cent in 2006 
(RO123mn) compared to 2005 and has witnessed an 11 per cent growth for the same 
period to bring the total life and non-life premium to RO140 million. The 
insurance market has mirrored the country’s economic growth quite vividly.”<br>
<br>
<b>Major trends</b><br>
A key facet of Oman’s insurance market is that life insurance segment’s 
penetration is significantly lower than that of non-life insurance. As the CMA 
data shows, in 2006, general insurance companies issued 629,277 policies 
compared to 595,525 the previous year, registering a growth of 5.7 per cent, 
while the life insurance companies issued just 19,325 policies compared to 
17,472 in 2005, growing at a rate of 10.6 per cent. Experts cite various reasons 
for this trend. <br>
<br>
As Rawny Khadr, VP, Dhofar Insurance Company reasons, “Personal insurance cover, 
which directly relates to life, travel, medical insurance, etc., is a direct 
product of insurance awareness and the average individual’s appetite/knowledge 
for insurance. Hence, the more the penetration increases, the more you will see 
the premium in life insurance increase.” Al-Harthy gives three reasons to 
explain the phenomenon. First he says, in Oman, life insurance is against many 
people’s beliefs. Secondly, although life insurance is common among non-Omanis 
but expats in Oman make up to 33 per cent of the population where they are 
usually covered by group life policies by their employers. Hence, they do not 
need to buy independent cover. Thirdly, in Oman, people hardly feel the need for 
life insurance because of traditional low inflation rate, free health care and 
free or very low cost of education provided by the government to Omanis.” 
Similarly, Debashish Maitra, Head - Bancassurance, BankMuscat, also gives three 
reasons for this trend. First, is low awareness of insurance products among a 
wide segment of the population. Also, the reach of the only local life insurance 
company and the only international life company is limited. And thirdly, life 
insurance is seen as expensive.<br>
<br>
Another characteristic of Oman’s insurance market is the declining market share 
of foreign insurance companies unlike that of local players. As the CMA figure 
shows, national insurance companies grabbed the lion’s share (83.7 per cent) of 
the total direct premium of the general insurance sector in 2006 compared to a 
81 per cent share in 2005, the share of foreign insurance companies declined 
from 19 per cent in 2005 to 16.3 per cent in 2006. As Andrew Woodward, GM, 
Muscat Insurance Company reasons, “Foreign insurers probably are not so much 
focussed on the large government contracts, but prefer to develop retail 
business, where they can try to leverage their international distribution 
experience and the strength of their brands to more effect.”<br>
<br>
In the life insurance segment as well, national insurance companies had a 
greater share of the direct premium pie than foreign insurance companies in 
2006. Khadr attributed this trend to factors such as product range, brand 
awareness, financial strength, price and service. “If you can find the balance 
between these factors, your market share will inevitably improve,” he assures. 
Explains Kamath, “Whilst there is preference for local insurers over foreign 
insurers in some cases, and local insurers have more branches spread over the 
country, there is no discrimination against foreign insurers as such. The 
declining market share of foreign insurers is probably due to the risk appetite 
of each company.” <br>
<br>
<b>Regulatory environment</b><br>
The CMA has introduced a number of important changes during 2005-06 in the 
insurance laws and regulations to keep the insurance industry attuned to the 
rapidly changing national and regional economies.<br>
<br>
These include:</font><ul>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">The Insurance Companies Law was amended to increase the paid-up capital to RO 5 
million for licensing;</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Approval for creating a guarantee fund for helping people injured by 
unidentified vehicles;</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">The GCC Draft Unified Insurance Law was completed while preparation of the draft 
unified GCC regulations is underway;</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">CMA approved the creation of the Omani Insurance Association to look after the 
interests of the employees in the insurance industry;</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">The Omani Unified Firm for Orange Card was established with a paid-up capital of 
RO500,000 shared by seven national insurers.</font></li>
</ul>
<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b>Challenges</b><br>
Oman’s insurance industry is home to a lot of competition and it is a case of 
too many players chasing too few customers. As Woodward points out, “The main 
challenge for Oman’s insurance market is still too many players in a relatively 
small market, coupled with an increasingly regular problem with storms. The 
market is growing at pace along with the economy, but the intense competition 
means that rates remain depressed. Rates here are among the lowest in the 
region, yet we have experienced far more problems with storms than other 
countries. In the short term of course this is good news for the customer, but 
the longer term risk is that some of the major international re-insurers, who 
are consistently losing money for some years now in Oman, will decide that there 
is no future in continuing. This would be a serious problem for the market.”<br>
<br>
A number of new insurance companies (both international and local) have set up 
venture in Oman. From the consumers’ perspective this could lead to better 
products and lower premiums in the short to medium terms, but too much 
competition may also give way to less technically sustainable rates that could 
affect the quality of service and products in long term. As Sanjeev Jha, CEO, 
Royal &amp; Sun Alliance Insurance Oman, cautions, “Given a strong regulatory 
environment around risk management and customer service, competition is healthy. 
However, it would become detrimental to the industry in case it results in 
unhealthy risk pricing and compromises in reinsurance security.”</font><p align="justify" style="margin: 3px 10px">
