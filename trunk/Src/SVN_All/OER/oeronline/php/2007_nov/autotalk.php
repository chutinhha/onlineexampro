<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Be all you can be<br>
</font><font face="Verdana" size="1">We take out the latest model to be launched 
by Cadillac for a long and exciting spin</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="auto.jpg" width="450" height="282"><p align="justify" style="margin: 3px 10px">
<b><font face="Verdana" size="1"><br>
</font></b><font face="Verdana" size="1"><br>
Given the various price points within the Dee segment, it is quite difficult to 
place the Cadillac BLS in parallel with the other players in the sub-RO10,000 
range. Hence, we have placed it in the Premium sub-section of the typical 
D-Segment, which is the most coveted. The entry point of the Caddy is RO10,900. 
The BLS stands with lot of frequently-used equipment, flaunty styling, and 
above-average performance.<br>
<br>
The BLS is not an upgraded, facelifted or liposuctioned model. Cadillac seems to 
place effort in preserving visual identity (like Audi): The BLS looks like its 
other siblings the CTS, STS, and the DTS. It is a Saab 9-3 underneath (more on 
that later), but GM’s designer Chip Thole made sure it looks nothing like other 
Swedish cakes. It has enough aggressiveness to appeal young male buyers and 
remain feminine enough to interest his girl friend. Cadillac’s prime years when 
they were better known as grandpa’s transportation is all but gone. Sharp edges 
all throughout the car, now emphasise cutting-edge technology and even fashion – 
as exemplified by the latest Escalade’s chrome couture.<br>
<br>
<b>Interior</b><br>
On the inside, the Caddy uses almost everything that General Motors have asked 
Saab to provide. However, due care has been taken to develop a new dashboard 
that complements the exterior. It is European in design and has very little 
Americanism to it. The BLS is well equipped with MP3 and auto-A/C. It has a 
three-spoke steering wheel and the analogue clock on the centre console. The 
interior layout is simple with clear dials, an uncluttered console and a 
high-quality Bose stereo. This is an area in which drivers will find little to 
criticise, thanks to comfortable, supportive seats and a clear dash layout. 
There’s a good range of adjustment in the seat and steering wheel to accommodate 
most shapes and sizes of driver, while the instruments are clear and easy to 
read. Front seats offer ample comfort; however, the BLS is not as spacious as 
some of its competition with limited legroom for passengers. Equipment level is 
high and one could chose between few variants which would suit their budget and 
needs.<br>
<br>
<b>Powertrain</b><br>
The Caddy comes with a turbo – as never before in history! This one is a 
high-performance 2.0-litre, four-piston unit that moves really well for its 
200hp output. The engine is very peppy, again a non-American trait, and responds 
with crisp anticipation of the driver’s foot. Oh, there is also a bigger 2.8, V6 
with 255hp – they popped that in there in case some hp-addict complains. <br>
<br>
The BLS has a range of Passive Safety features, including front, side and 
curtain airbags, activate head restraints for the front seats, pyrotechnic seat 
tensioners and force limiters on the front seats. The Active Safety features 
include StabiliTrak electronic stability programme which monitors the steering 
angle, yaw rate, wheel spin and road speed, correcting understeer and oversteer 
by automatic application of the brakes, control of the throttle until the car 
goes back on course. There is also ABS (anti lock brakes), traction control, 
electronic brake assist with (CBC) cornering brake control.<br>
<br>
<b>Drive</b><br>
Purely in terms of highway cruising, the BLS is very comfortable with minimal 
wind noise and relatively quite cabin. But obviously as a consequence, the BLS 
is handful when thrown around a corner. There is excessive body roll added to 
the steering with low feedback making the Cadillac less of a sporty car. 
Cadillac has not provided a switch to shut off the traction control systems 
thereby not allowing us to effectively explore the potential of the chassis of 
the BLS – this is anyway beyond the ‘duty’ of almost all its drivers. The engine 
does generate decent amount of power with very minimum feel of any turbo lag. We 
feel Cadillac sort-of took the 9-3 and retuned the suspension to behave 
‘American’ – we can’t really agree with that ideology. But as for normal every 
day driving the car has the qualities of up market executive in terms of looks, 
equipment levels and road presence.<br>
<br>
Unlike many of the other Cadillacs, the BLS is front-wheel-drive. However, 
enthusiasts tend to favour RWD for its superior balance, which generally 
speaking, aids handling (not the same thing as easier handling), improves 
steering feel and allows better power delivery from the engine to the wheels.
<br>
<br>
<b>Summary<br>
</b>Obviously all owners of cars like this want to make a statement. The BLS did 
not go out of the way to impress us – instead we were impressed by its 
sedateness. It seems to do everything an executive car should, but does it 
calmly. The most suitable owner would be a young male, who prefers to wear jeans 
to work and does not place brand image over his buck. The Caddy is worth it and 
he knows it.</font><p align="justify" style="margin: 3px 10px">&nbsp;<div align="center">
