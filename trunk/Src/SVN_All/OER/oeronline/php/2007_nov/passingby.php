</font><b><font face="Verdana" size="4">Zulu Kingdom calls</font></b><font face="Verdana" size="1"><br>
<b>Suave and articulate, with a pleasing demeanor and cheerful disposition, Boni 
Mchunu, Trade Marketing Manager of Tourism KwaZulu-Natal, who was in Oman 
recently, highlights the tourism potential of the province and the natural 
beauty it showcases in a freewheeling chat with Jayashankar Menon</b><br>
<br>
<img border="0" src="passingby.jpg" width="200" height="170" align="left" hspace="6" vspace="4">“It 
is a great honour for me to be in Oman. This is my maiden visit to this 
beautiful country. I am visiting all the governorates. It is love at first 
sight. People are warm and I am extending an open invitation to all Omanis to 
visit South Africa in general and KwaZulu-Natal in particular,” is how Boni sums 
up her visit to the Sultanate. “Really, it is in our interest to work with the 
Omanis and to improve the knowledge about South Africa, ensuring that everyone 
is aware about the paradise that South Africa is.”<br>
<br>
Tourism has really become the gold of South Africa. “Tourism has actually 
overtaken gold in terms of contribution to the country’s GDP. So, for me being 
here is a reflection of our commitment in making sure that we invest in this 
growing market. Globally, tourism grew by 4.5 per cent, but in South Africa, it 
grew by 13.9 per cent. We tripled what the world did. We think we are in a new 
market and everybody is discovering our beautiful country. The growth numbers 
are a testimony that globally, people are seeing this wonderful country,” Boni 
says.<br>
<br>
On safety apprehensions expressed by Omani business travellers, Boni agrees that 
“there is a huge perception that South Africa is unsafe. So, it is in our 
interest and it is the government’s top priority to make sure that we combat 
this whole thing about security. The government has invested a lot of money in 
policing and making sure that when tourists are there we offer them a secure 
environment. For us, tourism really is more than just brining in visitors to 
South Africa but it is about improving the well-being of our country. It is 
about job creation and transformation. South Africa has whole lot of history. 
The biggest challenge is increasing the awareness about our country in its 
totality. What we find is that people who have been to Cape Town think that is 
all there to South Africa. But there are eight more provinces and it is a big 
country. So, we are here to urge each and every one of you in Oman to come to 
our country. We have also strongly urged the government for a direct flight from 
Oman to South Africa. We hope our request will be positively considered by the 
government.”<br>
<br>
On the 2010 FIFA Confederation Cup, Boni says all preparations are on in full 
swing. “We know all eyes are on us to see that if we are going to stage a 
world-class event and we are positive that we will. I can proudly say that South 
Africa is on schedule. Out of the 10 stadiums identified by the FIFA, half of 
them already exist and the construction work on the rest began in January. We 
are sure that everything will be ready before June 2010.<br>
<br>
On a personal note, Boni says, “I remember when I decided to join the tourism 
industry it involved a major mindset change. My background had always been 
marketing and I was then working for Unilever. In marketing, all you need to 
understand are the fundamentals. But it is different ballgame when it comes to 
promoting tourism. Here, what we are selling is a promise: Come to South Africa, 
you will get to see this and that. But the nice thing is that it comes really 
naturally to me when I promote my country because it comes from within. I am 
very emotional about it because it is something that I live and aim for. Here, 
my marketing background is an added advantage.” </font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1">
