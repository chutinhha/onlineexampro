            <br>
            <b>The value of defining one’s core business: taking a fresh look at 
            your core business. <br>
            What is your company really good at doing? This is such a simple 
            question, that many managers are offended when asked. The answer is 
            anything but simple. Unless the managers of today’s companies know 
            this answer, it is likely that they will lead their companies in the 
            wrong direction.</b><br>
            <br>
            </font><b><font face="Verdana" size="3">M</font></b><font face="Verdana" size="1">ost 
            companies are created because someone had a good idea that was the 
            basis for its core business – the creation of a product or service 
            that was desirable and valuable to customers. As time passes and 
            companies grow, their expertise and focus is diluted. Product lines 
            are extended until there is not enough value left to be better than 
            competitive offerings. When that happens, the resources of the 
            company — time, money and people — are too often allocated to 
            projects and products that are not the company’s core business.<br>
            <br>
            The core business of a company relies on core competences (what the 
            company knows how to do) and capabilities (how it executes it). If 
            the company cannot execute well enough, it fails to convert its 
            knowledge into a commercially valuable result. When a company strays 
            from its core, it risks large investments in areas that are core to 
            a competitor, and that is a formula for disaster. But how is a 
            company to determine what its core business is? And how can its 
            management stay focused on that core? <br>
            <br>
            A series of questions will reveal the core business of the company 
            and these questions must be asked and re-asked since markets and 
            competitors are constantly changing. Once the answers are clearly 
            understood, management must take action – to concentrate on the core 
            business and find partners for support in non-core areas. <br>
            <br>
            A simple phrase helps to remember this important point: do what you 
            do best and outsource the rest. (Of course it is imperative that 
            ‘what you do best’ be something that is valuable to your customers.) 
            Asking the right questions in the right place and at the right time 
            is a critical part of defining one’s core business as well as 
            keeping that definition up-to-date as conditions change. <br>
            <br>
            <b>Questions to ask:</b></font></p>
                    <ul>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">What 
            (product, service, skill, etc.) was the company built upon, and what 
            fuelled its growth?</font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">Which of 
            the product lines (or unique points of differentiation) generate the 
            largest share of the revenue and profit?</font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">What does 
            the company do to provide value to customers – better than the 
            competition?
            </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">Is 
            advantage a product (or service), the way it is made, the way it is 
            delivered, or how much is charged for it? Or is it a combination of 
            these factors?
            </font></p>
                      </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Who to 
            ask:</b></font></p>
                    <ul>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">First, 
            ask a cross-section of management, but do it in a way that permits 
            anonymity, to ensure the most honest answers possible. <br>
            <b>Tip:</b> Employees will always tell the boss what they think the 
            boss wants to hear. They are afraid to be bearers of bad news. 
            Phrasing the questions requires skill. Some ways to do this: 
            anonymous surveys, third party interviewers, web questionnaires 
            (with no ‘cookies’ to tag respondents) etc.
            </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">Next, ask 
            lower level employees, since their perspective is based on more 
            direct customer contact than management has.
            </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Obviously, ask customers—both current and prospective ones—and be 
            sure to ask at multiple levels, not only buyers or executives.<br>
            <b>Tip:</b> You must get multiple perspectives if the results are to 
            be accurate and actionable. Executives must question peer-level 
            customer executives, and questions must be open-ended to elicit the 
            most useful response. Sales must ask buyers too, but in this case 
            there is always an element of negotiation. For this reason, such 
            enquiries are often best done by third parties.
            </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">Finally, 
            ask other stakeholders: suppliers, hourly workforce, investors, 
            communities, and so forth. The more perspectives you can get, the 
            better your understanding of the core business will be.
            </font></p>
                      </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>How 
            often to ask:</b><br>
            There is a fine line between too seldom and too often. Change is 
            occurring faster than ever. New products and services are being 
            introduced constantly, and each alters the value perception of 
            customers. One important time to ask is when there is a quantum 
            change in the competitive balance, whether caused by new products, 
            new competitors or major changes in market conditions. In the 
            absence of large-scale changes, these questions should still be 
            asked at some logical regular interval. The best questioning process 
            is one that fits in the natural cycle of the business. <br>
            <br>
            <b>Tip:</b> A good rule is to ask at least as often as the 
            introduction cycles of new products, or at least as often as 
            customers alter their selection of products and suppliers. Take your 
            cue from your customer's decision cycles and time frames. Ask in 
            advance of their decision cycles, not after them. This makes any 
            action a proactive one instead of a reactive one. <br>
            <br>
            Anytime there is a meaningful discussion, it is a good time to ask 
            such questions. When a sales call is made on a customer, ask then. 
            When a visit to a supplier is made, or a major supplier conference 
            is held, ask then. When groups of employees gather for a 
            communications meeting on the status of the business, ask then. <br>
            <br>
            Most importantly, LISTEN to the answers and hear what is said, not 
            what you hoped the answers would be! <br>
            <br>
            Finally, there is a caveat. A current industry leader never develops 
            the innovation that replaces its core business. Current industry 
            leaders are so intent on protecting the past of their core 
            businesses or so focused on perfecting the present core business 
            that they never invest the time to find the future. <br>
            <br>
