            of the day’s job, they are washed using food grade detergents and 
            hot water using high pressure sprays. The products are handled in 
            hygienic manner with highest standards in production process. We 
            have about 30 tests conducted for every batch of chicken product 
            that comes out of our processing plant, thereby maintaining highest 
            quality standards that we can proudly call Omani Standard and Omani 
            Product Quality.<br>
