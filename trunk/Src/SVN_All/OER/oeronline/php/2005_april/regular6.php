            <b>Could you please throw some light on how A'Saffa maintains 
            hygiene standards while rearing poultry? </b><br>
            Bio-security is the highest priority for any poultry farm. Our plant 
            fully adheres to HACCP standards – we are currently working on 
            obtaining the certification. It is built according to international 
            standards and the equipment, technology and machinery used are fully 
            European. <br>
            <br>
            Our Bio-security measures start from the gates of our farm, where 
            every vehicle is sprayed with disinfectant, every individual working 
            in each farm has to adhere to a very strict bio-security practice. 
            Our farms are well separated from each other to counter any 
            exigencies or diseases. The chicken that are hatched are vaccinated 
            as per our standard programs, the buildings that they are housed are 
            washed and disinfected, the bedding material that is used are 
            fumigated before chicks are put in.<br>
            <br>
            Wherever there is a contact with the product, all workers use 
            gloves, head covers, masks, etc. RO water is used for washing and 
            cleaning of the chicken and is void of any contaminants. The 
            chickens are raised in free, cool environment and they are not 
            caged. We raise our chickens in a very natural way.<br>
            <br>
