</b> </font><b><font face="Verdana" size="3"><br>
</font></b><b><font face="Verdana" size="3">Z</font></b><font face="Verdana" size="1">urich 
is a microcosm of Switzerland: immaculately clean, organised, panoramic, lofty, 
and above all, remarkable for what it has achieved regardless of its size. From 
banking, watches, chocolate and cheese to environmentalism, social activism and 
the Red Cross, it seems there is nothing they don’t excel at. <br>
<br>
Due to pressing commitments in Muscat and late notice, I had exactly five 
working hours within which to apply for and obtain a visa from the Swiss 
Consulate in Muscat. An impossible task, I told myself, but nevertheless 
gathered the necessary paperwork and, hoping against hope, arrived at the 
consulate minutes before it was due to close for lunch only to discover that I 
had forgotten an important document in my office. <br>
<br>
I had all but given up when a benevolent front-office consulate staff told me 
that she would accept my application, but would hand me over the visa in the 
evening once I produced the document. I must say that I was elated – the Swiss 
charm was already working! <br>
<br>
I collected my visa and tickets (Swiss International Air Lines operates a daily 
flight from Muscat to Zurich via Dubai) well in time in the evening, and was in 
fact seen off at the airport by Peter Schilter, Swiss Air GM – Oman, who was at 
his helpful best that evening, guiding passengers to the correct boarding 
terminal and assisting ones that required help. <br>
<br>
Once on board, the rest of the group joined me in Dubai, where the flight takes 
a 45-minute halt, before leaving for the onward journey. Besides the other 
scribes, we were joined by Felix Rodel, Swiss International Air Lines’ Area 
Manager (Middle East, Pakistan &amp; Iran), who proved to be an invaluable 
escort/guide to Zurich, also known as Downtown Switzerland. <br>
<br>
<b>Living it, loving it</b><br>
Switzerland is in the heart of Europe, and Zurich is its largest city, on the 
northern end of the Lake of Zurich. It has been a city since the 10th century 
and is charming and multilateral. It has the world’s fourth largest stock 
exchange and largest gold bullion market. Most Swiss banks have their 
headquarters in Zurich, and a fifth of the country’s national income is 
generated there. <br>
<br>
Although it is a city of over a million people, Zurich feels like a quaint Euro 
village at the edge of a crystal-clear lake with the Alps towering beyond. This 
setting along with the fairy tale-like towers and stone buildings of the town 
makes it look like something out of a dream. You can even guzzle the delicious 
mountain water straight from the fountains! For an ideal archetype of a city, it 
would be hard to find one better suited. <br>
<br>
<img border="0" src="zu2.jpg" align="right" hspace="5" width="150" height="131">What 
else would you expect from a city whose inhabitants have included Albert 
Einstein, Vladimir Lenin, Carl Jung, James Joyce, Thomas Mann, Karl Marx, and 
Tina Turner? The Swiss speak Swiss German, an interesting dialect that most 
Germans can’t understand. Despite the Swiss reputation for order and 
introversion, people are friendly and most speak English. The standard of living 
is high, as are the prices, and everything is top quality from the food to the 
clothes. One wonders if they realise how good they really have it. The answer is 
probably yes, since Swiss citizens have always been at the forefront of helping 
developing nations, encouraging world peace, and inspiring self- respect. Very 
few countries in the world do it better. <br>
<br>
Getting there is half the fun is what they say, and indeed it is so when 
travelling with the Swiss International Air Lines. Despite taking off an hour 
late due to technical delays, we reached Zurich dot-on-time at 6.20 in the 
morning. Immigration seems to be a breeze. They look at your passport, look at 
you, and just stamp it. Welcome to Switzerland, smiles the man behind the 
counter in a faultless accent. <br>
<br>
While I knew it would be cold and so undertook the journey well prepared in 
terms of warm clothing, I wasn’t ready for the chilly wind that whizzed across 
my face at –12 degrees centigrade the moment we stepped out of the airport. It 
was uncharacteristically cold in Zurich at that time of the year (early March) – 
in fact, it was snowing, and the forecast was more snow. Who was complaining, 
though?<br>
<br>
<b>Transport track</b><br>
We were almost immediately transported to our hotel through Best Link bus 
service, with all of us keeping our digcams busy, clicking away snow-covered 
buildings. Once in the cosy confines of the Intercontinental Zurich 
(approximately 30 minutes by bus from the airport), the hotel’s Serena Zehnder 
(Area Director of Sales &amp; Marketing, Switzerland) and Karine Schweizer 
(Assistant Director of Sales) left no stone unturned to make sure we were 
comfortable.<br>
<br>
Not willing to waste even a single moment, we dumped our luggage in our 
respective suites (courtesy Switzerland Tourism), refreshed ourselves after a 
long but comfortable journey, and were all ready moments later. We picked up our 
four-day First Class Swiss Pass and stepped out of the hotel to explore the 
city. <br>
<br>
The Swiss Pass/Swiss Youth Pass (ranging from a 4-day 2nd Class Pass for CHF245 
[approximately RO82] to a month-long 1st Class Pass for CHF810 [approximately 
RO270] less 25 per cent discount in case you are aged 16-26) offers unlimited 
travel throughout all trains, buses, boats, and trams running in the country. 
The other options are Swiss Flexi Pass, Swiss Transfer Ticket and Swiss Card (www.swisstravesystem.com).<br>
<br>
Whether you’re travelling by train, boat or post bus, a glance at the 
surrounding countryside is enough to capture the cultural wealth and 
geographical variety of this country – it all makes for an exquisitely varied 
travel experience, where mere miles separate glaciers from palm trees. The Swiss 
Travel System is not only the most pleasant way to get to know the country: it’s 
also the safest and most comfortable. <br>
<br>
We took a train from the nearest station and headed straight for Zurich’s 
Hauptbahnhof (meaning Main Train Station). In the heart of Switzerland’s banking 
capital, Hauptbahnhof is like walking a wire between two very different worlds. 
The inclination is to head south toward the chichi shops of the Bahnhofstrasse – 
a shopping boulevard that’s considered one of the most expensive in the world, 
boasting all the stereotypical Swiss materialism you could ask for in the form 
of pricey watch shops and gourmet bonbon boutiques. <br>
<br>
<b>Cityscape</b><br>
For an edgier insight into the city, though, exit north from the train station 
and venture toward the ‘‘other side of the tracks.’’ The area known as 
Zurich-West centres on the formerly down-and-out Langstrasse, the city’s 
long-time red-light district that has embraced its ethnic diversity, toned down 
its seedier side and emerged as the epicentre of cool. <br>
<br>
Designer togs fill one of the hip boutiques in the once downtrodden area called 
Zurich-West. You won’t encounter the fur coat crews here, but there’s still 
plenty of stellar people-watching. Students drawn to the area for affordable 
housing stroll the streets alongside recent arrivals from Brazil and Kosovo, the 
young and hip as well as aspiring artists. <br>
<br>
If you’re after a drink, you’ll be glad to know that this part of town is rife 
with watering holes. With a few exceptions, the middle stretch of the 
Langstrasse, where most of the area’s sleaze cinemas and cabarets are located, 
caters primarily to the beer-pounding bunch in dark dives. For a more refined 
experience, stick to the bars and clubs on the north and south ends of the 
Langstrasse. <br>
<br>
When it comes to eating, the ubiquitous Thai, Ceylon, American and Middle 
Eastern takeaways are affordable – and welcome alternatives to the overpriced 
fondue restaurants in Zurich’s more touristy areas. Zurich is also home to 
Adagio – the smallest restaurant in the world, accommodating just one couple!<br>
<br>
Zurich is a breath of fresh air. One of the cleanest cities on the planet, its 
water is so clean that you can drink it from public water fountains (pity it was 
all frozen when I was there!). With almost half a million people, its sanitation 
standards are among the highest in the world. Against the refreshing backdrop of 
mountains, you can feed the swans along the lake. The Paradeplatz in the town 
centre is reportedly where the banks store their gold bullion — underground. 
Banking is very secretive, so, it's best to concentrate on other things the city 
has to offer.<br>
<br>
Switzerland’s independence and neutrality have long been honoured by the major 
European powers, and the country was not involved in either of the two World 
Wars. The political and economic integration of Europe over the past 
half-century, as well as Switzerland’s role in many UN and international 
organisations, has strengthened its ties with its neighbours. However, the 
country did not officially become a UN member until 2002. Switzerland remains 
active in many UN and international organisations, but retains a strong 
commitment to neutrality.<br>
<br>
<img border="0" src="zu4.jpg" align="left" hspace="5" width="150" height="67"></font><font face="Verdana" size="3"><b>S</b></font><font face="Verdana" size="1">witzerland 
is a prosperous and stable modern market economy with low unemployment, a highly 
skilled labour force, and a per capita GDP larger than that of the big Western 
European economies. The Swiss in recent years have brought their economic 
practices largely into conformity with the EU’s to enhance their international 
competitiveness. Switzerland remains a safe haven for investors, because it has 
maintained a degree of bank secrecy and has kept up the franc’s long-term 
external value. Reflecting the anaemic economic conditions of Europe, GDP growth 
dropped in 2001 to about 0.8 per cent, to 0.2 per cent in 2002, and to –0.3 per 
cent in 2003.<br>
<br>
Due to its location, Switzerland has traditionally been and will continue to be 
at the crossroads of major international trade routes. From Switzerland, the 
gateway of Europe, you can reach all major cities of the Continent in two hours 
or less. This is one of the reasons why Switzerland became a centre for 
international business contacts and organisations.<br>
<br>
Zurich is the commercial centre of Switzerland while Bern is its political 
capital. Basel is the domicile of renowned pharmaceutical and chemical 
companies, and Geneva is the headquarters of numerous international 
organisations. Lausanne and St. Gallen are world-renowned education centres, and 
Lucerne is a famous meeting point of culture and tourism. Smaller agglomerations 
with an economic profile are Lugano as a banking and tourism centre, Winterhur 
with a medical technology base, and Neuchatel with electronics and 
micro-technology centres.<br>
<br>
The country is landlocked by Germany, Austria, Italy and France, and most Swiss 
speak German, followed by French and Italian, and less than 1 per cent speak 
Romansch. Switzerland has produced, per capita, more Nobel Prize winners, and 
registered more patents than any other nation on earth. And yes, of course, it 
is a direct democracy – Switzerland provides the strongest example of modern 
direct democracy as its citizens have the right to pass laws, veto laws and 
withdraw support from a representative at any time.<br>
<br>
In the past 120 years, more than 240 initiatives have been put to referendum. 
The populace has been conservative, approving only about 10 per cent of the 
initiatives put before them; in addition, they have often opted for a version of 
the initiative rewritten by government. Single majorities are sufficient at the 
town, city, and state (canton and half-canton) level in Switzerland, but at the 
national level, ‘double majorities’ (first, the approval by a majority of those 
voting, and, second, a majority of states in which a majority of those voting 
approve the ballot measure) are required to ensure any citizen-made law’s 
legitimacy.<br>
<br>
<b>When to go</b><br>
You can visit Switzerland any time throughout the year. Summer lasts roughly 
from June to September, and offers the most pleasant climate for outdoor 
pursuits. Unfortunately, you won’t be the only tourist during this period, so 
prices can be high, accommodation hard to find and the mainstream sights 
crowded. You’ll find much better deals and fewer crowds in the shoulder seasons 
of April-May and late-September-October. If you’re keen on winter sports, 
resorts in the Alps begin operating in late-November, move into full swing 
around Christmas, and close down when the snow begins to melt in April.</font></p>

