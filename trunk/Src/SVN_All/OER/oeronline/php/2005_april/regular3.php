<img border="0" src="tt6.jpg" align="right" hspace="4" vspace="1" width="150" height="113"><b>LG, 
Nortel successfully test new mobile service </b><br>
South Korea’s LG Electronics has successfully tested a next-generation 
high-speed mobile phone service with its Canadian partner Nortel Networks. The 
trial runs on the HSDPA (high speed downlink packet access) service mark a new 
era of cell-phone users visually communicating, enjoying two-way games and 
watching TV while moving by car or train, it said. The live test calls were 
conducted at Nortel’s lab in France. According to sources, the HSDPA service 
will eventually ensure data transmission at a maximum speed of 14 megabits per 
second, which is about seven times faster than the mobile phone services now in 
use. </font></p>
