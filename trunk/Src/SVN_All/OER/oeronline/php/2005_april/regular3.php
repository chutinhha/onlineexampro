<p class="MsoNormal" align="justify"><font face="Verdana" size="1">
<img border="0" src="tt2.jpg" align="right" hspace="4" vspace="1" width="154" height="130"><b>Group 
disables Apple iTune restrictions </b><br>
A group of US and Norwegian programmers have joined forces to crack Apple’s 
digital rights management software when downloading iTunes. They have developed 
a program called PyMusique, which allows users to legitimately enter the Apple 
site and buy iTunes, but then strip out the blocks that restrict the use of 
those tunes. The software lets people purchase songs from Apple’s iTunes music 
store for the standard price of 99 cents, but also lets customers save those 
tracks on their computers before they are protected by digital rights management 
software, which prevents widespread file copying. <br>
<br>
Their move may arouse the wrath of the music moguls as well as Apple, which so 
far hasn’t said anything about its copyright system being breached. PyMusique 
was initially written for Linux users but has now been developed for Windows 
users too, presenting an even bigger challenge to the music industry. <br>
<br>
The programmers behind PyMusique are Travis Watkins, Cody Brocious and Jon Lech 
Johansen. Norwegian Johansen was responsible with others for bringing out 
software six years ago that allowed users to copy film DVDs. He successfully 
contested legal action brought against him in Norway for that software, which 
still circulates on the Internet. All three programmers deny that PyMusique 
breaks any copyright law or Apple's user contract rules. </font></p>
