<b>Apple Computer backs Blu-ray </b><br>
Apple Computer Inc. announced last month it will join the Blu-ray format for 
next-generation DVDs, which is being supported by an alliance of companies led 
by Sony Corp. and Matsushita Electric Industrial Co. The Blu-ray Disc format is 
competing with the high-definition DVD format to become the major technological 
standard for next-generation DVDs. Toshiba Corp, NEC Corp and other companies 
developed the competitive HD DVD format. <br>
<br>
While current optical disc technologies such as DVD, DVD±R, DVD±RW, and DVD-RAM 
use a red laser to read and write data, the new format uses a blue-violet laser 
instead, hence the name Blu-ray. Despite the different type of lasers used, 
Blu-ray products can easily be made backwards compatible through the use of a 
BD/DVD/CD compatible optical pickup and allow playback of CDs and DVDs.</font></p>

