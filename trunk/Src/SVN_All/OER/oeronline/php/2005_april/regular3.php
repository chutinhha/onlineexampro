<img border="0" src="tt4.jpg" align="right" width="100" height="97"><b>Sony 
Ericsson launches Roving Camera </b><br>
Taking pictures with your mobile phone is no longer confined to what is visible 
through the viewfinder on the phone. Sony Ericsson’s new Bluetooth controlled 
camera, ROB-1, can move around freely—forwards and backwards—steered wirelessly 
by the joystick or keypad on your mobile phone or as for P900/P910, with the 
touch screen, enabling you to see on your phone screen exactly what ROB-1 is 
looking at. You can then capture these images using your phone just as you would 
a normal photo. <br>
<br>
The ROB-1 can rove around for a distance of up to 50 metres from the user, 
streaming video to the phone’s display. The device’s three wheels and spherical 
shape combined with clever camera technology to make it agile and flexible with 
a wide field of vision. Eleven centimetres in diameter, ROB-1 can move forwards, 
backwards, look around corners, pivot on the spot or tilt the camera 70º upwards 
and 20º downwards. So, the next time you see some bright lights at about the 
height of your ankle, you’d better watch out! </font></p>

