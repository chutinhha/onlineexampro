<p align="justify" dir="ltr"><font size="4" color="#FF0000">
<font face="Verdana">Case for COSO</font></font><font face="Verdana" size="1"><br>
<br>
<b>Afshan Patel provides an introduction to the Committee of Sponsoring 
Organisations and its relevance to Oman</b><br>
<br>
<img border="0" src="ind.jpg" align="left" width="150" height="188"></font><b><font face="Verdana" size="3">M</font></b><font face="Verdana" size="1">ost 
companies listed on the Muscat Securities Market (MSM) are working on their 
annual reports and corporate governance compliance reports this time of the 
year. Most listed companies in Oman do not have formally documented internal 
control systems, though most would have internal control measures in place. From 
a very broad perspective, even having a system that produces periodical 
financial statements is an internal control measure that most companies would 
have in place, while further measures like having a list of all laws and 
regulations that apply to an entity and having a legal officer sign off those 
that have been complied with may be a control of a higher order that not many 
listed companies may have.<br>
<br>
Whether the implications of the requirement of Article 10 are fully appreciated 
by all the directors of companies listed on MSM or not, this Article of the CCG 
truly confers a significant responsibility on them. For the board of directors 
to take responsibility for the internal controls of the company, as a starting 
point, companies should have their internal controls documented in an ‘Internal 
Controls’ manual or a ‘Procedures Manual’. Once the controls are documented, the 
directors can at least know what they are. The COSO guidance on internal control 
can be used to assist in effectively formalising and documenting internal 
controls. <br>
<br>
<b>Knowing COSO</b><br>
COSO is an acronym for ‘Committee of Sponsoring Organisations’. It is a 
voluntary organisation (www.COSO.org), sponsored by the following five 
professional organisations:</font></p>

                    <ul>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">American Accounting 
Association (AAA)</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">American Institute of 
Certified Public <br>
Accountants (AICPA)</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Financial Executive 
International (FEI)</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Institute of Internal 
Auditors (IIA) and</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Institute of 
Management Accountants (IMA)</font></p>

                      </li>
</ul>

<p align="justify" dir="ltr"><font face="Verdana" size="1">Although sponsored by 
the above institutions, COSO works independent of them. The Committee describes 
its mission as “Improving the quality of financial reporting through business 
ethics, effective internal controls and corporate governance”. From time to 
time, COSO comes out with publications that deal with the above or related 
issues. A COSO publication, ‘Internal Control – Integrated Framework’ can be 
used as guidance in designing, formalising or improving internal control systems 
by companies in Oman. <br>
<br>
The framework defines internal control as a process effected by an entity’s 
board of directors, management, and other personnel, designed to provide 
reasonable assurance regarding the achievement of objectives in the following 
categories:<br>
<br>
• Effectiveness and efficiency of operations<br>
• Reliability of financial reporting<br>
• Compliance with applicable laws and regulations<br>
<br>
What strikes me as important in this definition is that the parties responsible 
for the controls form an integral part of the definition. Therefore, it has to 
be understood, appreciated and absorbed that the board of directors, management 
and the employees are all in this game of internal controls, together. In case 
of a corporate failure or lapse, no member of any of these groups will be 
entitled to claim ignorance of what the internal controls of their company were 
or whether they were being complied with or not.<br>
<br>
The next part of the definition relates to the areas of an entity to which the 
Internal Controls should apply. In designing internal controls, the fundamental 
economic principle of cost benefit will be an important consideration. One will 
not put in place an expensive control to monitor a minor or remote risk.<br>
<br>
Internal controls in an organisation should ensure that all the operations are 
carried out efficiently and effectively. This is quite a comprehensive phrase 
and each entity will have to design control mechanisms to suit their operations. 
This one phrase would have been sufficient to ensure a good internal control 
structure in any organisation. However, even with sufficient operational 
controls in place, it is possible that the reported financial information may 
not be reliable; therefore, there is the necessity to specifically state the 
need for controls that ensure the production and reporting of reliable financial 
information. <br>
<br>
Again, compliance with laws and regulations is an aspect that can get overlooked 
even in the most efficiently run organisations and hence the need to 
specifically consider it and ensure suitable controls for compliance. In 
organisations operating beyond local geographical boundaries, it is essential to 
have control mechanisms that ensure compliance with laws and regulations in each 
of the geographical areas of operation.<br>
<br>
In effect and practice, how do the roles of these different players shape up in 
this game of internal controls? In corporate real life, the management and 
employees would design and install internal controls and the directors would 
have to be fully aware of them, what they are, what purpose they serve, the 
consequences of these controls being contravened and, of course, all concerned 
will have to satisfy themselves that the controls have been effectively in 
operation for the year they report upon.<br>
<br>
The COSO Internal Control Framework also identifies five components of internal 
control. These five interrelated components and the various issues that come 
under the first three components, as identified by COSO, are as follows:<br>
<br>
<b>1) Control Environment</b></font></p>

                    <ul>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Integrity</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Ethical values</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Competence</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Management philosophy 
and operating style</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">The way management 
assigns authority and responsibility</font></p>

                      </li>
</ul>

<p align="justify" dir="ltr"><font face="Verdana" size="1">The Control 
Environment is the edifice on which an internal control system is developed. It 
comprises the values of the entity and these values emanate from the top 
management. It is also referred to as the ‘Tone at the Top’. This tone that is 
set at the top permeates through the entire organisation. For example, if an 
ethical tone is set at the top, it is difficult for unethical practices that may 
develop down the line to be sustained for a long time. Conversely, an unethical 
tone at the top can very soon spread throughout the organisation and become 
endemic. <br>
<br>
Organisational values are generally palpable and are felt by all stakeholders 
and third parties dealing with the organisation. It is essential that a good 
internal control system start with good corporate values that display high 
levels of integrity and ethics and a management philosophy and style that 
reflects the fact that it is committed to achieving organisational goals while 
upholding an ethical corporate value system. <br>
<br>
There is no place for incompetence in the commercial world and so, none in a 
good internal control system either. The way management assigns authority and 
responsibility conveys very valuable messages down the corporate ladder. It can 
show whether the management is empathetic, consistent, results-oriented, 
proactive etc. and a management that is all of the above will be able to 
maintain a good internal control system.<br>
<br>
<b>2) Risk Assessment</b></font></p>

                    <ul>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Establishment of 
objectives</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Identifying external 
and internal factors that can pose a risk to the achievement of the 
organisations objectives</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Determining how risks 
are to be managed</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Management of risks 
associated with change </font></p>

                      </li>
</ul>

<p align="justify" dir="ltr"><font face="Verdana" size="1">One of the main 
objectives of a good internal control system is to enable an organisation 
achieve its objectives. A risk factor is defined as anything that can prevent an 
organisation from achieving its goals. In this context, a good internal control 
system will have to start with the identification of the organisational goals 
and the factors that can hinder the achievement of these goals and put in 
adequate checks, balances and control measures to mitigate the risks.<br>
<br>
<b>3) Control Activities</b><br>
Control activities would include those policies and procedures that help ensure 
that management directives are carried out. They include:</font></p>

                    <ul>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Approvals</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Authorisations</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Verifications</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Reconciliation</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Reviews of operating 
performance</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Security of assets</font></p>

                      </li>
                      <li dir="ltr">

<p align="justify" dir="ltr"><font face="Verdana" size="1">Segregation of duties</font></p>

                      </li>
</ul>

<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
<b>4) Information &amp; Communication</b><br>
There is innumerable amount of information produced by business entities in 
today’s world. Further, there is a lot of communication that takes place within 
an organisation and between an organisation and the outside world. Most of the 
communication channels develop out of necessity and are semi-formal. <br>
<br>
As part of a good internal control system, communication channels need to be 
formalised and documented. This will include what reports are produced, their 
frequency, who takes responsibility for their accuracy and timeliness, who signs 
off on them etc. Controls relating to information and communication can be 
directly related to one of the objectives of a good internal control system, 
which is ‘to provide assurance that the financial reporting is reliable’. Strict 
and effective controls over Information and Communication will, among other 
things, increase the assurance that the financial reporting is reliable. <br>
<br>
Lack of communication has been a cause for corporate failures and has also been 
an excuse for directors to shirk responsibility for corporate failures in the 
past. A lot of unnecessary information also gets produced in organisations. 
Having controls on information produced and circulated and looking at them from 
time to time will also help to eliminate unnecessary production of information 
and contribute some element of resource savings.<br>
<br>
<b>5) Monitoring</b><br>
Like with everything else, an internal control system also needs monitoring. You 
can’t just design it, install it and leave it to flourish or deteriorate. It 
needs constant monitoring to ensure that it is still relevant, still effective 
(serving the purpose it was put in there for) and still remains the best way to 
control the process, procedure, transaction or other aspect it was put in to 
control in the first place. <br>
<br>
The employee working with a particular control feature, the supervisors, the 
management, the internal auditor, the board of directors and the external 
auditor all form a part of this monitoring process and the order in which they 
have been listed is no random order – it is an order in which, in my opinion, 
they should take responsibility for having a control and monitoring it. <br>
<br>
Although from an external entity’s point of view, it would be the board and the 
management who would be first held responsible for any failure of controls that 
may have significant impact on external parties, within an organisation, 
responsibility for the controls should take a bottom-to-top approach.<br>
<br>
<b>Empowering employees</b><br>
Most controls are good and are also employee friendly. It is the way they are 
presented to an employee that is often the beginning of a long tussle or an 
underlying current of displeasure between management, internal auditors and 
employees. Often, organisations that do have formal internal controls or are 
even a step ahead in having them formally documented get an external consultant 
to do so. This is enough to give the employee a feeling that something that is 
going to be thrust or imposed upon him is in the making. <br>
<br>
The consultant will often meet with the top management and have a few 
perfunctory, ‘necessary to be complied with in order to sign off’ meetings with 
operational employees. Then, one fine morning, the management will call all the 
employees to a dreary meeting, not the nice lunch or dinner meeting that they 
would have with the consultant to mark the completion of the project, and tell 
the employees that this is the internal control document or procedures manual 
and that this will have to be followed and periodical checks will be made by 
internal and external auditors to ensure that they are in compliance.<br>
<br>
I am not against engaging consultants to do the job; generally, they are better 
equipped to take a holistic view and have a better feel for the overall control 
environment than somebody from within the organisation. Neither am I against the 
lunches or dinners! But I definitely feel that the approach can be modified to 
help the employee take greater ownership for the controls. <br>
<br>
The employee should first understand that the controls are there to assist him 
in doing a job that is efficient and less prone to error. The tone throughout 
the exercise should be positive, emphasising the controls more as tools of 
assistance and performance enhancers rather than management enforced mechanisms 
to check on employees. The entire internal control system should be woven in a 
way that every level of employee is able to understand how his job and the 
controls that he works with integrate into the overall fabric of the 
organisation and its objectives. An internal control system that has been 
developed on these lines will have greater success and, more essentially, 
greater ownership and compliance.<br>
<br>
How can company directors in Oman ensure that they satisfactorily comply with 
the requirement of article 10 of the CCG?<br>
<br>
The knowledge of what internal controls set out to achieve, what good internal 
controls are, what the internal controls of any specific company are, what plans 
the management has to test these controls, and what the results of the testing 
are will help the directors in effectively complying with the requirement of 
Article 10 of the CCG. <br>
<br>
Management’s plans to test the controls can be part of the detailed annual audit 
program wherein specific controls that are being tested are identified and 
reference is drawn to where these controls lie in the Internal 
Control/Procedures Manual. Risk-based audit reports that actually identify 
risks, identify the control feature put in to mitigate the risk, state that the 
control has been tested, state the outcome of the test and give a recommendation 
for improvement or change if required will also help compliance with Article 10.<br>
