<img border="0" src="inc.jpg" align="left" width="150" height="143">The insurance industry in the Sultanate is going through a transitional period, 
which is expected to result in more sophistication within the sector. An OER 
Spotlight<br>
<br>
</b> </font><b><font face="Verdana" size="3">W</font></b><font face="Verdana" size="1">hile 
existing players are trying to launch innovative products on the one hand, a few 
more foreign firms have entered the field on the other. Moreover, the government 
may bring in more regulations in an apparent move to facilitate growth and 
efficiency. Some of the national and foreign companies are contemplating to 
launch new products to cater to the special needs of the market besides trying 
to form alliance with commercial banks to create new delivery channels.<br>
<br>
Following are the five national companies that dominate the Sultanate’s 
insurance market: <br>
• Al Ahlia Insurance Co.<br>
• Dhofar Insurance<br>
• Muscat Insurance Co.<br>
• National Life<br>
• Oman United Insurance <br>
<br>
<img border="0" src="inc1.jpg" align="right" width="200" height="108">Foreign firms like New India Assurance, American Life Insurance (Alico), 
Northern Assurance, Norwich Union, LIC International and Royal &amp; SunAlliance 
Insurance complement these national companies. Companies like New Indian 
Assurance are looking at potential avenues like health insurance. There is an 
increasing demand for such products in developing countries like Oman as the 
cost of medical treatment has been increasing and the government agencies are 
gradually withdrawing from the healthcare sector due to mounting cost. This 
responsibility is taken up by insurers world over and the same phenomenon is 
observed in some of the Gulf countries as well.<br>
<br>
Dhofar Insurance became a leading player dealing in general insurance after the 
erstwhile ONIC Insurance transferred its local portfolio to the company. After 
the collapse of ONIC Insurance almost four years ago, all insurance firms have 
raised premium to make their operations viable and efficient. For instance, 
vehicle insurance premium forged ahead in the past three to four years. In fact, 
the insurance industry was able to convince the government that prior to 2000, 
there existed an unhealthy competition among major players, which had adversely 
affected their profitability. <br>
<br>
Some foreign insurance companies have recently entered the local market either 
through chief agency agreement or by way of joint venture. International firms 
can now set up shop in Oman easily since it has joined the World Trade 
Organisation (WTO) a few years ago. There are more than nine foreign companies 
in Oman operating both in life and general insurance for several years. <br>
<br>
With the formation of the Gulf Insurance Agencies (GIA, the chief agents of LIC 
International), competition is hotting up in life segment, especially among 
non-resident Indians (NRIs). The company has been offering insurance that caters 
to the special needs of NRIs working in the Sultanate. GIA has been getting a 
good response from Indian expatriates for its two popular products – Pension Pan 
and Professional Education Plan. <br>
<br>
Royal &amp; SunAlliance is another firm that formed a joint venture recently to 
expand its operation in the Sultanate. The multinational insurance giant formed 
the joint venture in association with WJ Towell &amp; Co. and OHI group. The company 
has been in operation in Oman for the last three decades through an agency 
agreement. <br>
<br>
American International Group, a US-based insurance multinational, recently 
extended its insurance coverage to property and marine segments. Earlier, Alico 
was giving coverage only for life. The collapse of ONIC Insurance, which was the 
leading general insurance firm in the country at one point of time, has left a 
vacuum in the market and the foreign companies are trying for a slice of the 
cake. <br>
<br>
The association between insurance companies and commercial banks has helped in 
promoting insurance products in remote places where insurance companies do not 
have branch network. Commercial banks that sell insurance products include 
BankMuscat and Oman International Banks. <br>
<br>
It is generally believed that there is a good potential in vehicle insurance 
business in view of growing demand for cars and the aggressive marketing 
strategy adopted by leading dealers. The new gas-based industrial ventures, 
which have reached an advanced stage of implementation, enabled national 
companies to get a very good business in the last one year or so. But these 
companies go for reinsurance cover with foreign multinationals, resulting in 
premiums generated within the country going out of Oman.<br>
<br>
The government has already formed a separate department within the Capital 
Market Authority, after transferring it from the Ministry of Commerce &amp; 
Industry. This is aimed at helping the insurance industry to achieve a healthy 
growth.<br>
<br>
There is a need for establishing a training <br>
institute for moulding insurance professionals. This would bring down training 
cost of national companies, which are now spending a lot of money for this 
purpose.</font><font face="Marlett" style="font-size: 5pt">g</font></p>

