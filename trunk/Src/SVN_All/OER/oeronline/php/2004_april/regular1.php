<p align="justify"><font face="Verdana" size="4" color="#FF0000">The audit trail</font></p>

<p align="justify"><font face="Verdana" size="1"><b>As the number of companies 
in Oman setting up internal audit departments increases, Afshan Patel looks at 
the international standards in place for the professional practice of internal 
auditing</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im1.jpg" align="left" hspace="5" width="250" height="167">Internal 
auditing in the private sector is a relatively recent phenomenon in Oman. Good 
corporate governance requires that organisations have an internal audit set up. 
This may take the form of one auditor or a department with many staff headed by 
a chief audit executive or director of internal auditing. The size and structure 
of the department will depend on the size of the organisation, nature of 
operations and its management culture. Management seeking to set up an internal 
audit department to merely comply with a regulatory requirement may make do with 
one person irrespective of the size or requirements. Yet organisations should 
normally ensure that the internal audit department is equipped to perform at its 
full potential. <br>
<br>
The mark of a profession is the existence of a regulatory or authoritative body 
that oversees the quality of services rendered by its members, provides redress 
to parties who may be aggrieved by the action of its members, sets standards for 
operations, codes of ethics and conducts disciplinary proceedings against 
members at fault. The US-based Institute of Internal Auditors (IIA) regulates 
the internal audit profession. <br>
<br>
The Oman Chapter of IIA was established in June 2003. The current membership of 
the chapter is about 150. IIA has a set of standards to guide internal auditors 
in their profession. The standards have come into effect very recently and are 
called the International Standards for the Professional Practice of Internal 
Auditing. The IIA states that compliance with these standards is essential if 
the responsibilities of internal auditors are to be fulfilled. <br>
<br>
<b>The progress made </b><br>
Today's concept of internal auditing has undergone significant evolution. 
Internal auditors have had to experience these dramatic changes first hand. 
Interestingly, the current concept of an internal auditor is that s/he shares a 
commonality of interests with shareholders, the Board and senior management and 
works to achieve the same goals as the organisation they are auditing work for. 
An unambiguous understanding of this concept will help management increase its 
acceptability of the internal audit process and help raise the value of the 
profession which has undergone some fallout from corporate scandals as discussed 
in an article in the last issue. <br>
<br>
Thus, great emphasis is usually paid to the internal audit activity being 
perceived and existing as a service that adds value to an organisation. 
Historically some managers have tended to consider internal audits as 
postmortems and hence of little relevance to a forward-looking company. Most, 
however, agree that an audit is a very important business process. As the output 
of the internal audit department Ð an audit report Ð is intangible and doesn't 
directly get converted to revenue, the task of demonstrating the value addition 
is challenging. Further, to be of real value to the organisation, the audit 
recommendations have to be of preventive value rather than an analysis of events 
that should not have taken place. Unfortunately it is easier to assess losses 
from events that should not have taken place but which took place than to 
measure the benefits of a preventive action recommendation. <br>
<br>
The first step in meeting the challenges of the new image internal auditor and 
working in accordance with the standards, is for the profession to undergo a 
paradigm shift and keep in mind that in order to improve the relevance of the 
service, the concept of 'value addition' has always to remain in the forefront 
while planning, executing and reporting on the audit. This can take the form of 
direct monetary savings, greater efficiency, improved morale, or any other 
positive outcome from the audit recommendations. Clearly, an audit report using 
such an approach would have a more positive and greater impact on the decision 
makers in an organisation. <br>
<br>
<b>Auditing the auditors</b><br>
International standards require an internal audit department to have an Internal 
Audit Charter that formally describes the purpose, authority and responsibility 
of the department and serves as a mandate for conducting the audit. A typical 
Charter would have a mission statement for the department, state how the 
department is going to achieve its independence and objectivity. There would be 
a section on scope, authority accountability and the standards that would be 
applied in the performance of the audits.<br>
<br>
One of the basic prerequisites for an effective internal audit service is the 
independence and objectivity of the auditors and the department. Independence is 
the ability to work without undue influence from other internal or external 
elements while objectivity is the mental state of being and remaining unbiased 
in obtaining and evaluating evidence and drawing conclusions from evidence. As 
internal auditors are employed by the organisation under review, attaining both 
independence and objectivity is a challenging task. In such situations the 
internal auditor has to demonstrate an extreme degree of maturity and 
responsibility in not jumping to conclusions without tangible evidence. <br>
<br>
<b>Conducting an audit </b><br>
The reporting structure of the internal audit department plays an important role 
in determining the independence of the audit function and its ultimate 
usefulness to the organisation. The Chief Audit Executive should report to a 
fairly high-level authority in the organisational hierarchy to ensure unhindered 
availability of information, prompt response to concerns and issues and 
responsible action on reports, while being able to maintain independence. 
Generally the Chief Audit Executive reports to the audit committee in its audit 
functions and to the Chief Executive Officer for administrative purposes. <br>
<br>
The standards set out three specific areas in which the audit function should be 
independent; the scope of the audit, the performance of the audit and the 
communication of results. The scope refers to the area or function to be 
audited, the period to be covered, the degree or depth of the testing to be 
done. The management should not exert undue influence on the audit department in 
determining an area to audit or an area to exclude from an audit. Such an 
influence may be construed as managements' deliberate attempt to divert the 
attention of the auditor from an area that may require attention. <br>
<br>
However, this does not mean that management cannot request an audit in a 
particular function or ask the audit department to address any area of their 
concern. In fact, every well-designed audit plan should set out some audit hours 
to cater to ad hoc requests that may arise. Management is also involved in the 
planning of audits in that the Chief Audit Executive should discuss with 
management and the audit committee, its audit plan for the year once it has been 
finalised. Input from management may be considered and changes made as required.
<br>
<br>
The second area of independence is in the performance of the audit. The 
management of an organisation should refrain from influencing the auditors 
regarding the performance of the audit tests. Needless to say, auditors should 
be able to independently choose the type of audit tests to perform, the 
methodology to adopt in performing these tests, the timing of the tests and the 
extent of testing to be done.<br>
<br>
Independence in communicating the results of the audit would translate into, 'no 
influence from management regarding the issues reported, the phraseology used to 
report the issues and the recommendations'. Auditors should use this 
independence with utmost grace. By no means is this independence to be construed 
as a license for ex partie reporting. Auditing best practices require that 
reportable findings should be discussed with senior management to understand the 
circumstances in which they occurred. After such discussion the matter should be 
reviewed by the auditors before they decide whether the matter may be dropped, 
the communication of it revised or retained.<br>
<br>
While management has the primary responsibility to establish and implement risk 
management, control, and governance procedures, the internal auditor is charged 
with the responsibility to evaluate and contribute to the improvement of these 
features. Risk in this context, refers to any impediment that may prevent the 
organisation from achieving its objectives. From a value addition concept, the 
focus of today's internal audit is on risk based auditing. The audit plan 
scheduling functions to be audited, time budgets, staff requirements etc. should 
be based on the risk profile of the various activities of the organisation. The 
more critical an activity is to the achievement of the organisational goals the 
greater should be the emphasis placed by management in ensuring that suitable 
controls are in place to prevent events occurring that may obstruct the 
achievement of these goals and greater should be the audit resources directed to 
ensuring that such controls are in place, are working effectively and 
efficiently and are adequate to address the risk. Where suitable controls are 
lacking, the internal audit report should give constructive recommendations for 
the implementation of controls, demonstrating the likely risks from not having 
the controls. <br>
<br>
The standards specifically refer to the following areas where effective and 
adequate control mechanisms should exist. Controls to ensure: <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> The 
reliability and integrity of financial and operational information <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Effectiveness and efficiency of operations <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Safeguarding of assets <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Compliance with laws, regulations and contracts <br>
<br>
<b>Improving the audit process </b><br>
Standards, which deal with reporting, require that the internal audit reports be 
accurate, objective, clear, concise, constructive, complete, and timely. 
Wherever the auditor finds satisfactory working, acknowledgement of this could 
be included in the report to add a positive note without jeopardising the rest 
of the report. To be considered complete, the report should include the 
objectives, scope, conclusions, recommendations, action plans and an opinion 
either on each section or an overall opinion. Internal auditors should endeavour 
to perform their audits in accordance with these standards and state this in 
their report. Where the internal audit was not or could not have been conducted 
in accordance with the standards, this should be stated along with reasons for 
the inability to conform. The internal auditors' task is not complete with the 
issuing of the internal audit report. Monitoring and follow-up sessions are also 
an essential part of the job. This is done to ensure that the recommendations 
have been implemented or have been satisfactorily dealt with. <br>
<br>
<b>Conclusion </b><br>
The Chief Audit Executive should ensure that internal audit resources are 
appropriate, sufficient and effectively deployed. The human resources of the 
department (the internal auditors) need to keep their knowledge current and 
constantly updated. The standards require that they acquire continuing 
professional education. Any limitations the department may face in terms of 
resources and the impact of such restrictions should be conveyed to the audit 
committee. <br>
<br>
Lack of resources will become a lame excuse if presented as a reason for 
negligent work resulting in losses to the organisation. An auditor is less 
likely to be considered negligent if lack of resources and their likely impact 
were brought to the attention of the audit committee but were not suitably 
addressed and went on to result in inadequate audit work and consequent losses.
<br>
