            <p align="justify"><font face="Verdana"><b>
            <font color="#FF0000" size="2">Scaling Everest</font></b><font size="4" color="#FF0000"><br>
            </font></font><font face="Verdana" size="1"><br>
            <b>Ford has launched a strong contender to take a major share of 
            Oman's entry level SUV market. Nayl D'Souza finds out what measure 
            of success the 2004 Everest can bring</b><br>
            <br>
            <img border="0" src="im3.jpg" align="left" hspace="5" width="300" height="174">The 
            Everest is an exciting new Sports Utility Vehicle from Ford that was 
            introduced into Oman during Ramadan last year. By all accounts, the 
            local dealership had many enquiries beforehand from an exhibiting 
            model paraded a month earlier at The Motor Show. One of the 
            vehicle's key Unique Selling Points is its durability. Proof of this 
            can be found in marketing literature for the Everest which locally 
            is locked on to boosting the perception of the product as something 
            &quot;built tough&quot; and &quot;built safe&quot;. Based on the same rugged platform as 
            the Ford Ranger pickup truck, the Everest promises to deliver 
            exceptional performance in diverse driving conditions handling both 
            city and off-road situations comfortably. <br>
            <br>
            For Ford in Oman, the 2.6-litre Everest is a robust smaller sibling 
            that could, in time, take pride of place among its very popular 
            bigger brothers: the 4.0-litre Explorer, the 4.0-litre Mercury 
            Mountaineer and the 5.4-litre Expedition. <br>
            <br>
            Slip behind the wheel of the 2004 Everest and you'll be left with 
            little doubt that it can compete against rivals such as the 
            Mitsubishi Outlander and the Isuzu Trooper. With a price tag 
            starting from RO7,800, the competition in town will be under no 
            illusions that the great value which the Everest offers could drive 
            sales away and pose them a tough mountain to climb in the future.
            <br>
            <br>
            <b>Exterior</b><br>
            The Everest may be the baby of the Ford SUV family but it looks 
            serious and tough. You'll be totally convinced that it can do the 
            business even before you step inside. Probably the first thing that 
            you'll notice is that the Everest is chrometastic! The large chrome 
            grill and stylish bumpers are matched with chrome exterior side 
            mirrors, door handles, and roof rails. The second striking 
            characteristic of the Everest is its length which is close to 5 
            metres. <br>
            <br>
            There are large 15-inch alloy wheels and aero-halogen headlamps. For 
            its 4WD endeavours, the Everest has a front wheel tow hook, some 
            nifty mud flaps, and decent ground clearance at 210mm. While the 
            front end of this vehicle is unmistakably apart of the Ford family, 
            the rear is slightly unconventional with its spare tyre mounted on 
            the rear door and the taillights above it. A convenient rear step 
            bumper improves access to items on the roof rack. <br>
            <br>
            You can choose from two-tone colour combinations that mix blue or 
            two shades of green with silver metallic or pick one of six colours 
            (dark green, spruce green, champagne silver, dark blue, silver 
            metallic or white) instead.<br>
            <br>
            <b>Interior </b><br>
            Ford has spared no expense in creating a great value car equipped 
            with many features. The interior is roomy, even for seven people. 
            There's a 50/50 double-folding 2nd row seat for added convenience. 
            The 3rd row is removable with a flip-fold manoeuvre or can be folded 
            flat easily for large, bulky items. Air conditioning inside is 
            sectioned inside the cabin to make rear passengers feel cooler, more 
            quickly - basically rear occupants get their own A/C vents and 
            controls too. <br>
            <br>
            Equality is certainly a theme in the Everest. Rear occupants also 
            get the same benefits when it comes to cup holders (yes, there are 
            seven cup holders in this vehicle). This SUV is also fitted with 
            power windows, door locks and side mirrors adjustment. Meanwhile, 
            there is plenty of storage such as map pockets behind seats and door 
            panels as well as overhead consoles for flexi-items and sunglasses.
            <br>
            <br>
            The inner door handles are chrome too - another nice touch. <br>
            <br>
            <b>Performance</b> <br>
            Powering the 2004 Everest is a 2.6 litre, 4-cylinder inline engine 
            developing a modest 134HP at 4,600 rpm. The fuel injected petrol 
            engine offers a fairly reasonable torque of 21.8 kg per metre at 
            3,500rpm. It's not particularly fast but the Everest makes up for it 
            with an impressive road manners and handling from a 5-speed manual 
            transmission. Admittedly OER's test drive could have been more 
            challenging. A few hills and a stretch along the beach seemed to 
            pose little trouble for the Everest. Certainly a ride into the 
            mountainous interior would have proved how effective the car was in 
            high altitude, high temperature terrain. But having said this, the 
            Everest was road-tested in the dusty, off-road climbs of the Hokaido 
            Mountains of Japan when it was being developed. I reckon the Everest 
            has become a hit among customers in the rural parts of Oman because 
            of the successful trailing it undertook in the inhospitable 
            environment of Japan. News that the engine is quite frugal when it 
            comes to fuel consumption will also bring cheer although the Ford 
            dealer couldn't provide fuel efficiency data for the Everest before 
            going to press. The fuel tank capacity is around 71 litres. <br>
            <br>
            <b>Safety </b><br>
            The safety features that are equipped as standard on the Everest are 
            very pleasing. These comprise of 4-wheel Advanced Braking System 
            (ABS), side impact beams, an anti-theft alarm system with engine 
            immobiliser and two front airbags (for driver and co-passenger). 
            Moreover, there are seatbelts for all seating positions including 
            the 3rd row seat. Additionally, the steering column is collapsible 
            in case of an accident, there is a fire extinguisher fitted as 
            standard inside the vehicle and there are childproof door locks to 
            ensure your kids remain secure. <br>
            <br>
            The seating configuration in the Everest provides tiered searing for 
            all occupants, providing excellent visibility for all passengers. 
            However, as with other SUVs, if you're going wadi bashing, this 
            means that your back seat passengers are more likely to get a 
            headache as they bump their heads on the cabin roof, that is unless 
            they are vertically challenged. <br>
            <br>
            <b>The finishing line </b><br>
            The Everest can provide a safe measure of rough and tumble but, more 
            than this, it is a value for money SUV positioned to attract mums 
            and dads with a large family who demand comfortable transportation.
            <br>
            <br>
            In short, the biggest selling points are that it can seat seven and 
            there is a dual control air-conditioning system with two additional 
            sets of A/C vents for rear seat passengers. It should, therefore, do 
            very well in Oman, particularly as summer approaches. More 
            importantly for people who care about good cars, the Everest looks 
            fairly classy and it provides a spirited performance on- and 
            off-road. <br>
            <br>
