<p align="justify"><font face="Verdana" size="4" color="#FF0000">Oman Ahoy!</font></p>

<p align="justify"><b><font face="Verdana" size="1">Oman's government has 
exciting plans to foster an ocean-based industry almost from scratch by 
purchasing LNG and oil carriers. OER highlights these plans and finds out from 
industry players whether they are on a voyage of recovery following a few 
challenging years</font></b></p>

<p align="justify"><font face="Verdana" size="1">Picture in your mind's eye a 
fleet of modern LNG carriers, supertankers, container ships, chemical carriers, 
and so on, all proudly flying the Omani flag and manned by Omani crew. Throw in 
for good measure a dry dock complex, maintenance yards, workshops, maritime 
training colleges, and an upgraded port infrastructure. And you have the makings 
of a shipping industry that promises to add value to Oman's mainstay hydrocarbon 
sector, open up new revenue streams, and generate employment on a significant 
scale. <br>
<br>
Thus, it's no surprise that Oman's government is embarking on an ambitious plan 
to establish an ocean-based shipping industry essentially on the lines 
visualised above. The nation stands on the threshold of an exciting era in its 
modern history as a new economic sector takes shape.<br>
<br>
In fact, the government is forging ahead full steam in realising this vision of 
an ocean-based industry. It has already established two state-owned enterprises, 
Oman Shipping Company, and Oman Ship Management Company, which will form the 
nucleus of a future shipping industry. These firms will own and manage the 
government's interests in a fleet of vessels, initially centring on the 
transportation of LNG, but which is later planned to be diversified into other 
areas of maritime trade. <br>
<br>
Helping Oman achieve its goal is one of the world's largest shipping companies, 
Mitsui OSK Lines (MOL), which has signed a comprehensive technical support 
agreement with the government. MOL's primary role is to support the government 
in a wide range of services, including vessel ownership, ship management and 
voyage operation, supervision of newbuilding LNG carriers, crew training, 
consultation and advice in contract negotiation, and the establishment of a 
national shipping company. <br>
<br>
The Japanese shipping giant, which operates a worldwide network of around 535 
vessels of a total tonnage of 34 million DWT, has already forged a strong 
partnership with the Sultanate's government. MOL and the government jointly own 
four LNG carriers, including SS Sohar LNG, which is currently being used for 
spot deliveries of LNG from Oman LNG's gas liquefaction plant at Qalhat. <br>
<br>
In March last year, both entities along with Mitsui &amp; Co Ltd., signed an MoU for 
the acquisition of SS Muscat LNG, a 145,000-cubic-metre-capacity carrier under 
construction at a Korea shipbuilding yard. Upon delivery, the new vessel will 
enter into a long-term time charter with the government. <br>
<br>
Just recently, MOL agreed a deal with the government that would give the 
Japanese shipping firm part ownership in two LNG carriers presently under 
construction at shipyards in Korea and Japan. The two tankers, due to be 
delivered in late 2005 and early 2006, will be deployed for the transportation 
of LNG from the Third Train project under development at Qalhat.<br>
<br>
For a nation that did not own as much as a modest-sized containership barely two 
years, Oman can today lay claim to developing from scratch one of the fastest 
national fleets in the region. However, compared to the fleet sizes of fellow 
GCC partners UAE, Qatar, Kuwait and Saudi Arabia, the Sultanate's four-strong 
flotilla is modest. <br>
<br>
But there is strong optimism that the national fleet will grow by leaps and 
bounds. Already, there is talk of the government planning the acquisition of 
another pair of newbuilding LNG carriers. From LNG transportation, the focus is 
expected to progressively shift to crude oil tankers operating out of Mina al 
Fahal, and even liquid carriers which will come calling at the Sohar Industrial 
Port when a number of petrochemical plants come on stream post-2006. <br>
<br>
In fact, the government's drive to establish an ocean-based shipping industry, 
with all the attendant payoffs for the wider economy, has come as cheery news to 
the local maritime sector, which has been hurt by a plethora of regional crises. 
The Iraq war, Middle East tensions, war risk insurance, higher port tariffs, 
mounting freight and fuel costs, and a sluggish economy to boot, have impacted 
negatively on the local shipping trade over the past several years. The quest to 
develop a full-fledged shipping industry is sure to transform the face of the 
local maritime sector as it opens up new business opportunities across the 
board. <br>
<br>
Keen to show themselves as enthusiastic partners in the government's efforts to 
develop the sector, the 20-odd shipping agencies of the Sultanate recently came 
together under one banner - the Professional Grouping of Shipping Agents (PGSA). 
Its goal: To assist and support the wider shipping and business community for 
the common benefit of the maritime industry and the national economy. <br>
<br>
PGSA will serve as an interface between the authorities, the import/export 
community, ship operators and their representatives, thereby strengthening this 
industry. It aims to build consensus within the community on key issues as well 
as play a key role in resolving emergent problems, such as war surcharge levies, 
port tariff increases, and other concerns that impact on business. <br>
<br>
It must be said in hindsight that Omani ports have been spared much of the grief 
that befell other ports in the region during the Iraq war. The Sultanate's 
premier ports, Muscat and Salalah, were exempt from the hefty war insurance 
premia imposed on vessels visiting, say, Yemen and other ports in the Upper 
Gulf. On the other hand, the country's geographic location outside the Gulf was 
seen by some shipping lines as a temporary safe haven for ships and cargo. At 
least one major shipping line diverted its ships from the Yemeni terminal of 
Aden to nearby Salalah, and has since stayed put. <br>
<br>
Chris Stone, General Manager, Inchcape Shipping Services, Oman, says: &quot;The 
shipping industry is an international business and Oman therefore has to compete 
on both a regional and global basis in order to attract and retain business. 
This to some extent has been recognised by the authorities and in turn some 
notable successes have been achieved with Salalah which has become a major 
international transhipment hub. It is also a good example of how the public and 
private sector can co-operate to achieve the desired result.&quot; <br>
<br>
&quot;The key issue will be to ensure that the ports of Oman remain an attractive 
alternative to other locations in the region by remaining competitive in terms 
of port and cargo handling costs and by keeping bureaucracy and 'red tape' to a 
minimum,&quot; he adds.<br>
<br>
Nevertheless, the succession of regional crises has failed to dampen the Omani 
government's ongoing programme for developing and modernising the country's port 
infrastructure. Sohar Industrial Port, which will serve a number of heavy 
industries, will receive its first cargo vessel in the coming weeks. Much of the 
infrastructure necessary to handle ships carrying project cargo for the planned 
industries is already in place. The presence of Rotterdam Port Municipal 
Management (RPMP) as a joint venture partner in Sohar Industrial Port Company 
augurs well for the future success of this hub. &quot;Sohar Port is a very positive 
initiative as it is targeting a niche market by attempting to attract specific 
industries that will add value to the economy of Oman and more importantly 
create direct and indirect employment for the people of Oman. The benefit to the 
local shipping community is in generating new business opportunities and an 
opportunity to grow in what has traditionally been a flat market&quot;, says Stone.
<br>
<br>
In the south, Port of Salalah continues to achieve one milestone after another 
in a quest to emerge as the port of choice in the region and as one of the 
world's largest container terminals. Last year, it handled two million TEUs of 
containers, while achieving an impressive average of 30 gantry moves per hours. 
In addition, the port's largest customer, Maersk Sea Land, and sister company, 
Safmarine, achieved a throughput exceeding one million box moves (roughly 1.5 
million TEUs) in one year, something never accomplished before in the Middle 
East, the Indian Sub-Continent and Africa region.<br>
<br>
These achievements have made the Port of Salalah one of the fastest growing 
ports in the world and one of the fastest in history to reach these milestones. 
The government, along with the operating company Salalah Port Services, is now 
pushing ahead with plans for a further expansion of the container terminal 
involving the construction of a fifth and sixth berth at an estimated cost of 
$238 million. The expansion will boost the combined handling capacity of the 
port to approximately 3 million TEU. The upgrade, along with the establishment 
of the Salalah Free Zone, will position Salalah for the long haul with a good 
balance between destination and transshipment cargo. <br>
<br>
Port Sultan Qaboos in Muscat is also planning initiatives to maintain its 
dominant role as the Sultanate's commercial gateway. In May last year, Port 
Services Corporation (PSC), the port authority, introduced revised tariffs for 
the first time since 1981 as part of a far-reaching effort to boost vessel and 
cargo traffic through improved port services. It is also weighing a plan for a 
major upgrade of its infrastructure. Towards this end, an international 
consultant has been appointed to assess future demand for the port's services, 
and also chart a plan for its expansion. <br>
<br>
In a bid to provide a range of services to users, the port authorities have 
initiated moves for establishing a passenger terminal, duty free area and other 
facilities at Mina Sultan Qaboos. A new reception area for cruise tourists, 
complete with dining halls and outlets offering heritage merchandise, is also 
envisioned. <br>
<br>
On the northernmost tip of the Sultanate, work is progressing on a multipurpose 
port equipped to accommodate a range of ships including general cargo vessels, 
feeder container ships, international passenger cruise ships and other military 
and service craft of the government. When completed in mid 2004, Khasab port is 
set to transform this northernmost town of the Sultanate into a vibrant hub for 
regional trade and cruise-ship based tourism. It will also boost maritime trade 
between Oman and Iran just north of the Hormuz Straits. <br>
<br>
Attention is also currently focused on the Sultanate's southeastern seaboard 
where an ambitious multipurpose port and dry dock complex has recently been 
given the green light at Duqm on the Wusta coast. The green-field port complex 
will be suitably equipped to provide a range of services to coastal shipping, 
including dry dock services, provisioning, and even bunkering. <br>
<br>
Significantly, Duqm will host the Sultanate's first dry dock, its remote 
location seen as ideal for the kind of activities associated with ship repair 
and maintenance. Envisaged is a facility catering to a variety of coastal 
vessels of around 35,000 DWT or lower. The government hopes that a dry dock 
complex will spawn a range of auxiliary businesses that in turn will generate 
jobs. <br>
<br>
Talking of employment, Omanisation is sometimes perceived as a difficult task, 
but some industry players have successfully embraced this challenge.<br>
<br>
&quot;I believe we might be the only shipping agent in Oman who have met with the 
required 60 per cent target for Omanisation. I would also like to mention that 
the government is committed to making improvements in this area. Schemes are 
underway which in the future will introduce more skilled labour in the field of 
shipping,&quot; says Dick Danielson, General Manager, Gulf Agency Company. <br>
<br>
To create employment opportunities, Oman will need to compete with neighbouring 
ports, like those in the UAE, more effectively. <br>
<br>
Says Stone of Inchcape Shipping, &quot;The close proximity of Oman to Dubai and other 
locations in the region has meant that Oman has lost business through its ports 
and airports to land transportation. It is important that this trend is reversed 
by the Shipping Community and the authorities working together to offer 
importers and exporters a cost effective and time sensitive cargo handling 
process.&quot; <br>
<br>
All these port upgrades, along with the ambitious plan for an ocean-based 
shipping industry, bode well for the future of the maritime sector. At long last 
Oman will be looking to its unquestionably proud seafaring heritage to revive 
some of its past glory as a pre-eminent maritime power.</font></p>

