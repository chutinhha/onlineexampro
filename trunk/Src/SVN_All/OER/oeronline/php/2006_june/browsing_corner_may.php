            </font><font face="Verdana" size="4">
            The brighter side of epidemics</font></b><font face="Verdana" size="1"><br>
            <br>
            <b>What point is the tipping point? OER delves deep into an 
            interesting book that precisely looks into this aspect of human 
            behaviour</b><br>
            <br>
<img border="0" src="browsing_corner1.jpg" align="left" hspace="5" vspace="5"></font><font face="Verdana" size="4"><b>H</b></font><font face="Verdana" size="1">ow 
            little things can make a big difference is a concept that is not new 
            to sociologists and economists, who have, at some stage or the 
            other, gone into detailed study or research on a particular problem 
            or phenomenon. But for everyone else — especially the casual reader 
            — The Tipping Point by Malcolm Gladwell would make interesting 
            reading. <br>
            <br>
            The concept of a tipping point itself is so important in one’s life. 
            It is almost like an individual or a society’s level of endurance. 
            It is a point beyond which things tip: they suddenly change for 
            better or worse. It is that point, which Gladwell examines so 
            lucidly, where, an epidemic or crime reaches such enormous 
            proportions that it starts threatening and interfering with 
            day-to-day life. Conversely, a point where the same crime rate, for 
            some strange reason or reasons, has suddenly started to ebb into 
            oblivion. In the same manner, fashion trends seem to suddenly appear 
            in ‘epidemic proportions’ and also disappear all of a sudden without 
            warning one fine day, as do certain brands in the market place.<br>
            <br>
            To prove this, he has beautifully examined the rise of the Hush 
            Puppies and Airwalk shoe brands as well as crime in New York and the 
            problem of syphilis in Baltimore. He takes the reader to the sets of 
            two children’s shows — ‘Sesame Street’ and ‘Blue’s Clues’. He 
            studies how a high tech company in Delaware works and examines case 
            studies to make sense of how suicide and smoking suddenly crop up 
            among adolescents in certain areas. <br>
            <br>
            <b>Three Factors</b><br>
            There are three characteristics for a tipping point. Contagiousness 
            that is like a ‘virus’ is the foremost. The second distinguishing 
            characteristic is that little causes have big effects. And finally 
            the fact, that change happens not gradually but at one dramatic 
            moment. In his bestseller, Gladwell has explained three rules of 
            epidemics that follow such as — the law of the few, the stickiness 
            factor, and the power of context. <br>
            <br>
            Even psychologists may want to agree with him when he advises not to 
            attribute everything to ‘Fundamental Attribution Error’ and to take 
            into account the power of context in each situation. In the same 
            context, Muscat (once declared the second cleanest city in the 
            world) is a classic example. Is it clean because the people, both 
            local and expatriates, have high standards of hygiene and 
            cleanliness? Or is the city clean because it is consciously kept 
            clean by the Muscat Municipality? <br>
            <br>
            For this kind of a revolution to take place, a certain type of 
            person is required, Gladwell identifies these people as connectors, 
            mavens and salesmen — all different personalities, working in their 
            own spheres that contribute to the magical process of change. They 
            are ordinary folk but with a power to suddenly change and lead to 
            the tipping point. <br>
            <br>
            Gladwell, who has covered subjects such as, business, science and 
            medicine for Washington Post, and is now a staff writer for the New 
            Yorker, has done extensive research on social dynamics. He has come 
            up with a brilliant book no doubt. It answers two questions — why is 
            it that some ideas, behaviors and products start epidemics, and 
            others do not? And what can we do to deliberately start and control 
            positive epidemics of our own? <br>
            <br>
            In his own words, “The theory of Tipping Point requires that we 
            reframe the way we think about the world. I have spent a lot of time 
            in this book, talking about the idiosyncrasies of the way we relate 
            to new information and to each other. We have trouble estimating 
            dramatic, exponential change.” <br>
            <br>
            It is precisely this change that is absolutely certain in this fast 
            evolving environment. So if you are an educator, parent, marketer, 
            in business or a policy maker pick up a copy and read on. <br>
            <br>
            —Uma Devi Jadhav</font></p>
