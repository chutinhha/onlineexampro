</font><font face="Verdana" size="6">
<b>Bears grip MSM</b></font><font face="Verdana" size="1"><b><br>
Investors lost heavily, with industry and banking &amp; investment sectors reporting 
a heavy downfall</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b>T</b></font><font face="Verdana" size="1">he overall market underwent a 
downturn this month. The correction during the period brought the index down by 
around 450 points, reporting a close below the 5,000 psychological mark. <br>
The Industry Sector Index tumbled down to record double-digit loss, losing all 
accumulated gains of the previous month. Following the same direction was the 
Banking/Investment Sector Index that continued its weak performance and dipped 
further by 9.5 per cent. The Service Sector Index fell by 7 per cent, with 
sizeable volumes of around RO30 million. <br>
<br>
From the year to date perspective, the sector most affected by this month’s weak 
performance of the market was the Industry Sector Index. It has dipped by 17 per 
cent YTD. The MSM 30 Index lost ground, registering a drop of 3 per cent YTD. 
Banking/Investment Sector Index also followed the same trend, with YTD loss of 5 
per cent. On the other hand, the Service Sector Index managed to sustain its 
positive zone to show YTD growth of 1.5 per cent. <br>
<br>
Market breadth was heavily tilted towards the decliners as 63 stocks reported 
monthly losses compared to only 13 gainers. The total value of the equity market 
during this period crossed RO100 million, a jump of 62 per cent from last month. 
The number of shares that exchanged hands amounted to 101 million (shares), as 
compared to 73 million shares in the previous month. All this indicates the huge 
selling pressure and bearish sentiments across the retail investors during the 
month under review. <br>
<b><br>
Top Gainers &amp; Losers</b><br>
The gainers during this month were dominated by the industry sector. Muscat 
Thread Mills was the biggest percentage advancer as the share gained 21 per 
cent. Al Ahlia Converting Industry, Gulf International Chemicals and Oman 
Chromite appreciated by approximately 9 per cent. From the investment sector, 
Oman Holding International was at the second place, up 11 per cent.<br>
<br>
The top five losers comprised all three sectors. The least performing stock was 
Sohar Poultry that notched by 50 per cent to close at 500 Baizas. United Finance 
also dropped by 30 per cent with volumes amounting to RO1.6 million. From the 
industry sector, Al Jazeera Tube Mills and Oman Cement lost 29 per cent and 26 
per cent respectively. Al Jazeera Services fell by 27 per cent, with volumes 
amounting to RO3 million.
