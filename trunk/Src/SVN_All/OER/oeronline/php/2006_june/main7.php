<b><font face="Verdana" size="5">
Lead with passion</font><font face="Verdana" size="6"><br>
</font></b><font face="Verdana" size="1"><b><br>
David Batstone writes about key drivers that motivate people to keep going</b><br>
<br>
<img border="1" src="mgmt.jpg" align="left" width="115" height="143">Recently I sent out an invitation to a select few to test a new tool that I 
created, the TripleP Quiz — Purpose, Passion and Profit. A remarkable number of 
them took the quiz and offered me valuable feedback as to their experience 
taking it, the significance of the results, and how the tool might best be 
utilised. I would like to return the favor and deliver a post-quiz debrief on 
their collective results.<br>
<br>
Following the publication of my last book, I went on the road for two years 
talking to companies about significance and purpose at work. I discovered that 
when workers explain what motivates them, they keep coming back to three basic 
drivers: Purpose, Passion and Profit. So I designed a short inventory to 
identify how individuals lead with one of these drivers.<br>
<br>
I say “lead,” because we truly operate with a mix of motivations. But I found 
that nearly everyone I interviewed revealed a primary driver that shaped his/her 
experience at work. I aim not only to help workers learn more about themselves, 
but I hope to offer the workplace a language for job engagement and the range of 
motivations that inspire team members.<br>
<b><br>
The San Frascisco Origin</b><br>
The TripleP took a leap forward in a café on San Francisco’s waterfront. I 
regularly connect with Joy Anderson, President of Criterion Consulting, with 
whom I have collaborated on a range of projects over the years. While mulling 
over a few concepts to save the world — what else do you discuss in a café? — 
the conversation thread kept returning to our understanding of how people create 
meaning. I told Joy about the TripleP, and we spent the rest of the morning 
brainstorming about how to turn it into a tool that would be dynamic and easily 
accessible. The TripleP Quiz was born.<br>
<br>
It has been helpful over the past few months to run the quiz on members of fixed 
constituencies — our first client was a university alumni network in the Midwest 
— and then analyse the results. With each new organisation that runs the quiz, 
we can measure the validity of specific questions and the overall distribution 
of responses.<br>
WAG Sample Okay, moving on to the WAG list, one of the chosen few for this test. 
Not surprising, they are a very Passion-led community! Here are the top-line 
results:<br>
<br>
- 56 per cent of you lead with Passion<br>
- 26 per cent of you lead with Purpose<br>
- 18 per cent of you lead with Profit<br>
<br>
Compared to our first control group, the number of Passion-led individuals is 
almost 20 per cent higher. Obviously, the WAG community values inspiring and 
creative work. No matter how much a prospective employer or client entices them 
with the higher purpose of a job, or the tangible rewards of achieving, if they 
do not feel passionate about the activities that the position involves, they are 
not likely to find the job enticing. <br>
<br>
In other words, Passion-led people shiver at the thought of waking up to a month 
of Mondays and face a set of tasks that are uninspiring. Among WAG readers, the 
single question that received the strongest response was: “My job must allow me 
to learn and grow.” About 70 per cent of who took the TripleP Quiz marked 
“Always” to this statement.<br>
<br>
<b>Purpose-led People</b><br>
By and large, I meet Purpose-led people most often in the non-profit and civic 
sector. Don’t get me wrong; these individuals are not disappointed to take on 
creative tasks. But what inspires them is the larger mission of the enterprise 
of which they are a part. Purpose people do not fit into a one-size-fits-all 
box, however. While one person may want to find a cure for cancer, another 
Purpose person finds motivation for designing a new software. You want Purpose 
people to help drive the mission and core values of your organisation. They keep 
the enterprise on course.<br>
The Profit Camp<br>
<br>
The smallest slice of WAG readers fit into the Profit camp. Profit in this 
regard does not solely refer to bottom line financials. In a much broader sense, 
Profit-led people find meaning in achieving a set of determined deliverables. 
They are the ones who provide discipline and structure to the organisation. If 
you have ever started your own enterprise, you know the valuable role that 
Profit-led people play, especially once your operation began to scale.<br>
<br>
The more I engage with companies, the deeper I appreciate the range of 
motivations required to make an organisation healthy and successful. Individuals 
are not all wired the same; they find meaning in very different ways. 
Unfortunately, we do not always value the differences.<br>
<br>
Recently, I received an extremely cynical note response from a WAG reader who 
took the TripleP Quiz and proclaimed that Passion people are self-indulgent. In 
essence, this Purpose-led individual wanted to send the message: It is well and 
good to seek inspiration, but get over it people, because the world is full of 
suffering people. It is always a temptation for Purpose-led people to feel that 
any other motivation for meaning is inferior, if not a sell out.<br>
<b><br>
The Dilemma</b><br>
His dogmatic tone reminds me of a dilemma that a CEO presented to me recently. 
The company was a victim of its own success; it was experiencing wild economic 
growth. When the company launched over a decade ago, a very passionate founder 
attracted a first wave of employees who also believed fervently in the products 
of the company. Once the company passed the US$100 million mark in sales, the 
management team saw the need to bring in Profit-led people who could better 
discipline its operations. The early generation workers, of course, viewed the 
intrusion of the Profit-led people as a threat to their Passion-led corporate 
culture. The Profit-led people felt less than welcomed. For their part, they 
wondered how such a chaotic, undisciplined crew could have gotten so far in 
business.<br>
<br>
My challenge is help the employees see the value of an orchestra with many 
instruments. No organisation can sustain itself without a strong mission 
(Purpose), a creative and inspired dynamism (Passion), and clear set of 
achievements and deliverables (Profit). When any one of these values dominates 
in such a degree that it squeezes out the comfortable space that the others 
offer, then the organisation will falter. Those enterprises that value the 
uniqueness of their personnel, on the other hand, design work environments where 
productivity thrives.<br>
<i><br>
David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States.</i></font></p>

