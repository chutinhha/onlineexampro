            expansion. It has stepped forward to lead the establishment of three 
            finance houses in Europe, Asia and the USA. Musadag El Melik, Head 
            of QNB Al Islami, says the basic concept of Islamic banking is 
            something that is built on the ethical beliefs or principles of not 
            just Muslims, but also of the two other major religions — Judaism 
            and Christianity. “Usury is banned in all three religions. So we are 
            providing a service that is interest-free. So we can simply look at 
            this form of banking as ‘interest-free banking’ — as one economic 
            concept. Yes, there is more than just being interest-free, we will 
            not invest in what is prohibited — alcohol, tobacco, gambling etc. 
            But those may well be the principles of those who are not Muslims 
            too.”<br>
