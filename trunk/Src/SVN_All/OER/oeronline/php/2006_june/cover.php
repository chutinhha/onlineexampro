                <p align="justify" style="margin: 10px">
                <font face="Verdana" size="1"><b>Islamic Finance: Some Key 
                Concepts</b></font></p>
                <ul>
                  <li>
                  <p align="justify" style="margin: 10px">
                  <font face="Verdana" size="1"><b>The Principle Of Mudarabah
                  </b><br>
                  Mudarabah is a profit-and-loss-sharing contract whereby a 
                  professional manages the capital of a group of investors. In 
                  Islamic banking, the bank acts as a mudarib 
                  (entrepreneur-borrower) and manages the funds of the 
                  depositors in order to generate profits, in which the 
                  depositors share. The bank uses the depositors’ funds on a 
                  mudarabah (profit-sharing) basis. That is, the bank acts as 
                  both the entrepreneur-borrower on the saving side of the 
                  equation and as the rabbul-mal (owner of capital) on the 
                  bank’s investment, or asset portfolio, side.</font></li>
                  <li>
                  <p align="justify" style="margin: 10px">
                  <font face="Verdana" size="1"><b>The Principle Of Musharakah
                  </b><br>
                  The second pillar of Islamic finance, Musharakah, is similar 
                  to the conventional concepts of partnership and joint stock 
                  ownership. For example, Muslims seeking home financing at 
                  Shariah-compliant banks enter into contracts incorporating 
                  musharakah structures. The three most popular are 
                  declining-balance co-ownership, lease to own, and instalment 
                  credit.</font></li>
                  <li>
                  <p align="justify" style="margin: 10px">
                  <font face="Verdana" size="1"><b>Riba</b> <br>
                  In Islamic law, Riba is defined as excess, either in quantity 
                  or term, in the counter-values of whatever is 
                  exchanged—currency, commodities, goods, or anything else — in 
                  a variety of sales, purchases, swaps, or loans. In one 
                  explanation, riba is forbidden because it is predatory or 
                  exploitative, and produces profit at another person’s expense.</font></li>
                  <li>
                  <p align="justify" style="margin: 10px">
                  <font face="Verdana" size="1"><b>Risk Sharing </b><br>
                  Contrary to popular perception, Islamic banking is not 
                  monolithic, nor can Islamic finance be neatly summed up as a 
                  prohibition against charging interest (riba), as it is often 
                  portrayed in the media. Riba, as described in Islamic 
                  jurisprudence, is a forced, or demanded, exchange of 
                  quantities that gives one party an advantage over another. 
                  Because it can be predatory, riba is considered unjust. For 
                  example, riba in a loan is prohibited because the lender is 
                  guaranteed a profit whether the borrower has gained or lost on 
                  the transaction. Conventional mortgages are regarded as 
                  exploitative in part because the mortgage-holder does not 
                  share in the homeowner’s risk. </font></li>
                </ul>
