            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">What made Sohar Aluminium approach the 
            IF route? OER raised this question to Musab Abdullah Al Mahruqi, the 
            Deputy Chief Financial Officer of Oman Oil Company (the local 
            sponsors of Sohar Aluminium). Musab says:<i><b> </b></i>“We wanted 
            to tap the other markets too. We wanted to expand our options. We 
            were confident that such a scheme would work in the Omani 
            environment. As for the future, we do not have any problem in 
            approaching the IF route.”<br>
            <br>
            Clearing the cloud further is Usman Ahmed, Deputy CEO of Citi 
            Islamic Investment Bank E. C. (CIIB) in Dubai, a 100 per cent-owned 
            subsidiary of Citigroup. He reveals to OER that his bank has 
            recently closed the first Islamic tranche for the Omani issuer — the 
            US$ 260 million Islamic facility for the Sohar Aluminium project.<br>
            <br>
            There are other examples as well. One of the reasonably large 
            diversified groups in the oil and gas sector in the Sultanate is 
            reported to have borrowed money for a transaction on an Islamic 
            basis. AES Barka, the 427 MW gas fired power plant and 20 MIGD 
            desalination plant, is also reported to have borrowed some of its 
            project finance through IF. A real estate sector company, and some 
            leasing companies are also reported to be interested in considering 
            the IF route. <br>
            <br>
            Rajeev Singh, Partner, Ernst &amp; Young, Oman, informs that his firm 
            was approached by a consortium of developers bidding to the 
            government on the Sohar Independent Water and Power Project. (They 
            did not go on to win the bid). Ernst &amp; Young, Oman, were their 
            financial advisors and had approached an IF institution to put in 
            almost US$200 million worth of project finance. </font></p>
