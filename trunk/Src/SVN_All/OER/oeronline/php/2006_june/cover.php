            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">Has IF arrived in Oman? “I think it 
            has definitely arrived,” argues Rajeev. “It has been a force in the 
            region for sometime. Oman government has been very pragmatic in 
            consciously trying to avoid linking religion with business. We 
            understand that the regulator doesn’t have a problem if you have 
            these (IF) products without specifically marketing it under that 
            tag. IF transaction and lending has been taking place in Oman for 
            the past few years, but quietly, and without a lot of publicity. But 
            businessmen continue to use that. It’s now a way of life in the 
            Middle East.” <br>
            <br>
            Is the trend likely to pick up in the future? Rajeev believes it has 
            to; it’s a fact of life across the region. A big development for 
            Islamic banks, points out Rajeev, is that while in the past it was 
            confined to retail business, and later corporate, the recent times 
            have seen the arrival of the Islamic component for project finance. 
            “You now see sizeable IF component forming part of project finance 
            tranches. Anything between 20 per cent and 35 per cent of project 
            finance transactions now comprise of Islamic tranches,” he informs.<br>
            <br>
            However, Khalid sees the non-standardization of IF banking 
            procedures as the biggest hurdle for its growth in Oman. This, he 
            views, as a problem with the structural element of the IF concept. 
            The Abu Dhabi Commercial Bank or the Dubai Islamic Bank have their 
            in-house Shariah boards, he points out. Malaysia, where IF is 
            breaking new ground, has set the benchmark for standardization. “We 
            don’t have this,” he says.<br>
            <br>
            Khalid then points to the problem relating to the marketability of 
            IF products. Here, he sees a big problem with liquidity, which he 
            says, is an issue with both IF and conventional products. The 
            primary market is low. However, change of hands is not taking place 
            in the secondary market. Khalid cites the example of Sukuk (Islamic 
            leasing bonds), which has done extremely well in the primary market. 
            In the secondary market, Sukuk has the attractiveness but not the 
            liquidity. Khalid says the prospect of IF looks bright in the 
            short-term. Petrodollars have made the market more attractive. “But 
            only when the secondary market sees transactions taking place, that 
            liquidity and attractiveness will come to the IF market.” <br>
            <br>
            Rajeev, on the other hand, sees a silver lining with regard to the 
            IF terms and conditions, which have been improving in recent times, 
            for example the tenure of the loan, the sort of paperwork and 
            documentation required, and the level of compliance etc. “In recent 
            times, IF has become easier to understand and easier to facilitate. 
            IF now has standard packages so there is no need to bother about 
            checking compliance from scholars. Bankers arrange all that. The 
            transaction process has become very smooth now,” says Rajeev.<br>
            <br>
            The eased procedures and the heightened interest bring us to back 
            the basic question: what is happening on the&nbsp; regional and 
            global maps? How far has IF’s reach grown? <br>
            <br>
            <b>IF: Third Generation </b><br>
            In fact, Muslim societies have always looked to means of conducting 
            their banking and investment affairs according to the Shariah, the 
            sacred law of Islam. However, the discovery of oil in the Arab world 
            in the 1930s brought about radical changes, stepping up the 
            challenge of compliance in the global economy. <br>
            <br>
            Today, there seems to be a growing interest in IF even in such parts 
            of the globe as Europe and North America, where Muslims are a 
            minority. And in the Middle East, IF may become a way of life! A 
            recent study illustrates that the Asia-Pacific region looks 
            promising as well, following estimates that Islamic banks will 
            account for 40 per cent to 50 per cent of total savings of the 
            Muslim population worldwide within the next eight to 10 years. <br>
            <br>
            Modern Islamic finance, clearly, has come a long way since it 
            appeared on the scene some 30 years ago. The industry is now seeing 
            a third generation of evolution. The concept as such started with 
            the creation of Islamic banks in the Middle East in the mid-1970s. 
            However, majority of their assets were either real estate or very 
            short-term, trading-type activities. <br>
            <br>
            The second generation lasted from the mid-1980s to mid-1990s. Now, 
            Islamic banks were developing a bit more sophisticated corporate and 
            retail banking structures, trying to come up with better products 
            and a better level of service. From the mid-to late 1990s to the 
            present day, is the time for the third generation of IF 
            institutions. Sophisticated product offerings mark this phase. <br>
            <br>
            The first Islamic investment bank was founded in Bahrain in 1997. 
            Since then, several Western banks have set up Islamic windows, 
            including UBS Group’s Noriba Bank, Citigroup’s Citi Islamic 
            Investment Bank, and HSBC Amanah Finance. It is not unusual, now, to 
            find conventional banking models operating side by side with Islamic 
            banking systems. <br>
            <b><br>
            What They Offer</b><br>
            Islamic financial institutions typically offer such traditional 
            Islamic banking services as investments in Murabaha (instalment 
            sales), equity investments that conform to Shariah precepts, real 
            estate investment programs, treasury products, foreign exchange, and 
            private wealth, and portfolio management. An Islamic fund of hedge 
            funds is only the latest milestone in the development of Islamic 
            finance, an opportunity lying somewhere between US$200 billion and 
            US$300 billion.<br>
            <br>
            Islamic financing in the GCC in general, and Dubai in particular, 
            has seen a surge recently following the launch of Islamic banking 
            services by regional and international banks. <br>
            <br>
            “In fact, Islamic financing has grown in double digits from a low 
            statistical base. At some point in the future, growth rates will be 
            slower because the industry is maturing,” points out Dr. Eckart 
            Woertz, Program Manager, Economics, of the Dubai-based Gulf Research 
            Center.<br>
            <br>
            Adds Dr. Woertz: “Islamic financing has quite some potential in the 
            Islamic countries and among migrant communities in the West. But 
            much of its future growth depends on how it will be able to copy 
            conventional banking products, including project financing, options, 
            short selling.”<br>
            <br>
            “This in turn might raise discussions about its specific Islamic 
            identity, as boundaries to conventional banking become increasingly 
            blurred.”<br>
            <br>
            For example one can argue whether murahaba constructions are some 
            kind of disguised interest and in fact treasury departments of 
            Islamic banks, which invest in such vehicles use the LIBOR (London 
            Inter-bank Offered Rate) of conventional banking as a benchmark.<br>
            <br>
            That may be so for now. But in future Islamic financial institutions 
            will come under increasing competition. Will they be able to 
            withstand such competition from the likes of Citigroup, HSBC and 
            Deutsche Bank?<br>
            <b><br>
            They could, says Dr. Woertz.</b><br>
            It is not too difficult for a conventional bank to ease into Islamic 
            financing. The similarities are greater than the differences, says 
            Dr. Woertz, who says it is about risk management and the time value 
            of money... “Once a Shariah board is in place and there is 
            administrative separation of Islamic/non-Islamic accounts, there is 
            no problem. All the bigger banks, including Citi, HSBC, Deutsche etc 
            are venturing into the field since there is increasing business.”<br>
            <br>
            But Islamic banks will have to keep initiating new measures with 
            products that will be able to maintain profitability.<br>
            “The huge profit margins of Islamic Banks will decline once 
            investors become more sophisticated and demand some kind of profits 
            or ‘disguised interest’ on their current accounts. So far, the 
            Islamic Banks mostly get deposits at essentially no cost while 
            operating profitably with them on the consumer financing and 
            murahaba markets.”<br>
            <br>
            But what needs to be done in order for the industry to expand and 
            still corner more share of the international finance is to integrate 
            value with sustainable growth.<br>
            <br>
            The Islamic finance sector needs to do more to encourage socially 
            responsible investing (SRI), a recent forum in Dubai was told.<br>
            <br>
            Of particular interest and key to the moral dimension of investing 
            will be the parallels drawn between Islamic Finance and Socially 
            Responsible Investing (SRI), a strong and growing force in Europe 
            and the US, which aims to balance sustainable value to both society 
            at large and the shareholders by investing in companies who meet 
            their exacting standards of Corporate Social Responsibility (CSR).
            <br>
            <br>
            “Islamic finance is rapidly maturing into a mainstream and powerful 
            financial industry, and Shariah-compliant disciplines are 
            accelerating into many new sectors,” according to Christianna 
            Tsiterou, Conference Director at IIR Middle East, organizers of the 
            9th International Islamic Finance Forum (IIFF) in Dubai.<br>
            <br>
            Probably the Dubai International Financial Centre (DIFC), which 
            plans to list Islamic instrumnets at some stage can play a key role 
            in the development of Islamic financing.<br>
            <br>
            Islamic banking and financial institutions intending to operate out 
            of Dubai International Financial Centre (DIFC) must ensure a clear 
            division between senior management and company-appointed Shari’a 
            Supervisory Boards (SSBs), according to Hari Bhambra, Senior Manager 
            - Supervision at DIFC Financial Services Authority (DFSA).<br>
            <br>
            While a company’s board would hold ultimate responsibility for the 
            organisation’s operations, including its general compliance with 
            DIFC’s regulatory framework, as well as its Shari’a aspects, the SSB 
            would be responsible for approving the Shari’a aspects and reporting 
            to DFSA, adds Bhambra.<br>
            <br>
            But future growth in the GCC Islamic financial services industry 
            hinges on developing and introducing standardisation and innovation 
            of products and services.<br>
            <br>
            Says Dr. Habib Al Mulla, Chairman, Dubai Financial Services 
            Authority (DFSA), ‘The Islamic financial services industry should 
            seek to be as innovative as possible in terms of products and 
            services that they offer. The industry would greatly benefit from 
            standardisation of Islamic services and products, and this would 
            promote greater consistency across the industry.”<br>
            <br>
            Industry experts point out Islamic firms would greatly benefit by 
            being able to respond more quickly to the needs of clients, and not 
            be delayed by the legal and Shariah approval process.<br>
            <br>
            The DFSA considered many jurisdictions when devising its regime for 
            the DIFC as a whole and specifically Islamic finance. With respect 
            to Shari’a, the DFSA chose to act as a Shari’a Systems Regulator 
            rather than a Shari’a Regulator.<br>
            <br>
            ”For the future, the DIFC has established the Islamic Finance 
            Advisory Council its responsibility is to begin work with the 
            industry to identify the gaps and issues in the market, and then set 
            realistic timetables with a view to improving the operating 
            environment for Islamic finance. This is just a start. As a 
            regulator, we recognise that we along with the market have to 
            continue to evolve,” adds Dr. Al Mulla. Of equal importance is the 
            Citigroup Dow Jones Islamic bond index recently launched, to help 
            create a benchmark for Sukuks“But Sukuks as such are far from 
            constituting a benchmark, most of the time they are 5-year floating 
            rate notes and fixed coupon and government sukuks are rare. Thus a 
            benchmark all across the yield curve is not possible yet,” said Dr. 
            Woertz of GRC.<br>
            <br>
            The Dow Jones Citigroup Sukuk Index includes investment-grade, US 
            dollar-denominated Islamic bonds-also known as Sukuk-issued in the 
            global market. The Index tracks seven issues: Islamic Development 
            Bank, Solidarity Trust Services Ltd, BMA International Sukuk, Qatar 
            Global Sukuk, Malaysia Global Sukuk, Sarawak Sukuk and Dubai Global 
            Sukuk.<br>
            <br>
            To be included in the Index, an Islamic bond must comply with both 
            Shariah law and the Bahrain-based Auditing &amp; Accounting Organization 
            of Islamic Financial Institutions (AAOIFI) standards for tradable 
            Sukuk. Once a bond meets these criteria, Dow Jones Indexes and 
            Citigroup will apply market-based criteria such as minimum maturity 
            of one year, minimum issue size of US$250 million, and an explicit 
            or implicit rating of at least BBB-/Baa3 by leading rating agencies.
            <br>
            <br>
            Bahrain is increasingly emerging as the undisputed capital of 
            Islamic finance. This notion is supported by continued advances made 
            in the sector. <br>
            <br>
            To begin with, some 30 Islamic financial institutions (IFIs) operate 
            in Bahrain. These include investment IFIs like Gulf Finance House 
            (GFH) or commercial banking entities such as Shamil Bank. Still, 
            other leading banks such as Citibank and Arab Banking Corporation 
            have dedicated units catering to Islamic finance in Bahrain. <br>
            <b><br>
            Supportive Institutions</b><br>
            To be sure, Bahrain is well placed to lay claim on being the primary 
            hub for Islamic finance by virtue of supportive institutions in 
            place. Already, Bahrain plays host to numerous institutions 
            promoting and organising Islamic banking activities, like the 
            International Islamic Financial Market (IIFM). Set up in November 
            2001 by several Muslim countries and Saudi-based Islamic Development 
            Bank, IIFM serves as a money market for Islamic institutions 
            worldwide. In turn, this is sustained through Liquidity Management 
            Centre (LMC), which helps realising IIMF’s objective. <br>
            <br>
            Set up in July 2002, LMC provides a platform for IFIs to manage 
            their day-to-day liquidity requirements. In particular, LMC manages 
            the sale of Islamic leasing bonds, better known as Sukuks. In short, 
            LMC intends to develop an active secondary market for short-term 
            Shariah-compliant treasury products. <br>
            <br>
            To add to it all, the International Islamic Rating Agency (IIRA) is 
            based in Bahrain. Set up in 2002 as a pioneering project, IIRA 
            extends ratings to IFIs and other Shariah-compliant products. As 
            such, the global banking industry’s necessary infrastructure is 
            based in Bahrain. <br>
            <b><br>
            Serious Regulations </b><br>
            Financial authorities in Bahrain regard Islamic banking sector as 
            one with extraordinary growth potential. Not surprisingly, the 
            government is keen to promote Islamic banking by providing a 
            supportive regulatory environment, with emphasis placed on high 
            standards, transparency and accountability. <br>
            <br>
            The Prudent Information and Regulatory Framework for Islamic Banks 
            (Piri) came into effect in 2002. Piri requires&nbsp; Islamic banks 
            to maintain a capital-adequacy ratio of 12 per cent. In fact, the 
            Accounting and Auditing Organization for Islamic Financial 
            Institutions (AAOIFI) is based in Bahrain too. According to 
            International Monetary Fund (IMF), Islamic finance houses anywhere 
            are mandated to follow standards set by AAOIFI. <br>
            <br>
            Other regulations are in place in dealing with asset quality, 
            management of investment accounts and liquidity. In 2003, BMA signed 
            a memorandum of understanding with the London Metal Exchange (LME) 
            to develop contracts and documents that abide by Islamic law. The 
            final aim of the deal is to assist IFIs conduct transactions on the 
            LME.<br>
