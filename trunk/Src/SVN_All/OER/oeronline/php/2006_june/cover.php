            <font face="Verdana" size="1">This brings us to the issue of the 
            market appetite for IF. Khalid Ahmed Alhosni, Managing Director &amp; 
            CEO, InfoMart (Commercial Information Market LLC), Muscat, feels the 
            market appetite has always been there.<i><b> </b></i>“Call it 
            Islamic finance or traditional funding, corporates need funds. Many 
            companies in Oman would like to avail themselves of IF,” says 
            Khalid. </font></p>
