            qualified personnel to run Shariah-compliant programs.<br>
            <br>
            Another obstacle is the lack of uniform standards. Differences in 
            approach can make it difficult for professionals who try to adapt 
            their financial business practices in one Islamic country to 
            another. <br>
            <br>
            Islamic banks looking to compete in the global economy must 
            eventually align themselves with Basel II, the international banking 
            standard. This means developing the necessary technological 
            capability and operating procedures. However, conventional banks 
            that already comply with Basel II and decide to enter the Islamic 
            banking business, must reengineer their processes to make their 
            activities Shariah-compliant. <br>
            <br>
