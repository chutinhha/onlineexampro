            Islamic banking, is the main contributor to Bahrain’s gross domestic 
            product (GDP). In 2004, the sector accounted for 24. 2 per cent of 
            the GDP in constant prices, up from around 19 per cent in 2003. 
            Activities related to the financial services sector, namely 
            investment banking, advisory and management services, grew by a 
            hefty 35 per cent in 2004.<br>
