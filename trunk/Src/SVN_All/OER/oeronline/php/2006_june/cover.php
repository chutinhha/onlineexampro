            Bright Future </b><br>
            The IMF believes that Islamic banking extends opportunities to 
            Bahrain’s financial services sector. The point was made during the 
            financial sector assessment programme (FSAP) review of Bahrain, 
            released in March. Says IMF: “The variety and popularity of Islamic 
            instruments has surged in recent years, driven mainly by the supply 
            of funds from savers who wish to invest in Islamic vehicles, and to 
            a lesser extent, by entrepreneurs seeking financing using Islamic 
            instruments. The growth is likely to remain rapid over the near 
            term.”<br>
