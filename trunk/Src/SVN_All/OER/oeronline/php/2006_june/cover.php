            the newly launched Qatar Energy City. In fact, Essam Janahi, Chief 
            Executive Officer of GFH, serves as chairman on the board of Qatar 
            Energy City, whose first phase will be built at the cost of US$1.6 
            billion. Besides, promoters expect to launch a stock market 
            dedicated to trading in oil and gas derivatives, as part of Qatar 
            Energy City project.<br>
