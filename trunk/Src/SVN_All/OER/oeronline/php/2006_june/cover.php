            Islami, Al Safa (Commercialbank) and Doha Islami (Doha Bank) join 
            Qatar Islamic and Qatar International Islamic Banks. “In countries 
            such as the UAE and Saudia Arabia, both such models of banking have 
            simultaneously existed for a long time. It should be construed as 
            ‘healthy competition’ as it keeps everybody on their toes in terms 
            of coming up with offerings that are value packed. Qatar is now 
            experiencing the same and I am optimistic that it will contribute 
            positively in all aspects,” says Mohammad I Mandani, Deputy CEO and 
            Head of Islamic Banking, Al Safa Islamic Banking. “Conventional 
            banks opening up Islamic branches only proves how popular the 
            concept has become. And this increased competition will only being 
            about improved quality in products and services,” according to a QIB 
            spokesperson. <br>
            <br>
            Islamic finance is indeed coming of age in Qatar. It is no longer a 
            unique or niche service, as its appeal crosses religious and 
            national boundaries. It is probably a little intriguing why this 
            concept of banking had not found wider favour earlier here. <br>
            <br>
