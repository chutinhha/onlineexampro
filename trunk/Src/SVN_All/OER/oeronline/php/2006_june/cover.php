            <br>
            It was an interesting disclosure by any yardstick. Talking to OER on 
            December 11, 2005 (the day Sohar Aluminium’s equity and technical 
            partners had gathered in Muscat to put their seal of approval to go 
            ahead with the development of the project in Sohar), Tony Kinsman, 
            the CEO, declared his company had the distinction of bringing in 
            Islamic finance to the shores of Oman. <br>
            <br>
            The comment, coming from a top CEO, perhaps, was a pointer to a 
            probable paradigm shift. Is this to be a trend? Is Islamic finance 
            (IF) on its way to Oman? <br>
            If we talk of the Muslim world, IF has been wildly successful in 
            Malaysia, accounting for 10 per cent of all banking operations 
            there. In the GCC region, Bahrain was the first to catch on to the 
            trend and promote the concept. And the UAE has seen a renewed push 
            in this sector in the recent times. <br>
            <br>
            Formally, Oman is not into IF. The Central Bank of Oman (CBO) does 
            not allow the local banks to deal in Islamic finance, banking or 
            insurance products. On that count, the Sultanate has a different 
            policy compared to its GCC peers like Bahrain, Saudi Arabia, Qatar 
            or the UAE. But companies operating in the Sultanate are 
            increasingly climbing on to the IF bandwagon. There is heightened 
            interest in IF in Oman. As of now, the regional banks have moved in 
            to fulfil this need.<br>
            <br>
            Sohar Aluminium’s case is, probably, a pointer to the shape of 
            things to come. The first Greenfield aluminium smelter project in 
            over a quarter century in the entire Middle East, Sohar Aluminium is 
            raising US$260 million of the total US$ 1.58 billion project, from 
            the IF route. This makes up for around 15 per cent of the total 
            project finance. For the first time in Oman, a capital project is 
            probably availing of IF of this size. </font></p>
