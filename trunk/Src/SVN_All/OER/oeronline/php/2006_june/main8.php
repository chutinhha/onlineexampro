<br>
Always think out of the box<br>
</font></b><font face="Verdana" size="1"><br>
<b>For P. Kumar, the General Manager of Al Jazeera Tubes, the best style of 
management is one that ushers in delegating responsibility and treads the 
innovative path </b><br>
<br>
<img border="1" src="lastpg.jpg" align="right" width="110" height="137" hspace="5" vspace="2"></font><font face="Verdana" size="4">I</font><font face="Verdana" size="1">
have always believed in out-of-the-box thinking. For me, this is “the” style of 
management. My core strength lies in human resources management (HRD). And even 
in HRD, I have always believed in thinking out of the box. In fact, when I 
joined Al Jazeera Tubes as its General Manager in 2003, I did not have any 
experience in the steel industry. But my 15 years in the banking industry and 10 
years in manufacturing had given me enough confidence to take up this challenge.
<br>
<br>
I have always preferred taking up a challenging assignment than working in an 
environment where you feel comfortable. People often ask why I left banking to 
join steel. After all, banking job is supposed to be a glamorous one! There is 
not much of tension; work starts at 8 o’clock; you leave by 2 o’clock; nobody 
calls you at night. But this is not exactly what I was scouting for. Somehow, I 
have felt more comfortable working in operations. <br>
<br>
It’s true that joining a steel company was a big challenge. But ultimately it’s 
the people who run a company. If you have self-belief, you can work anywhere. 
Even when I had joined banking, it was a totally new area for me. And, after 
all, steel makes people much more bolder and stronger! Doesn’t it?<br>
<br>
<b>Export: A Success Story<br>
</b><br>
We are the finalists in Oman Awards for Excellence 2005. (The Oman Awards for 
Excellence, created seven years ago, are the benchmark for the private sector’s 
performance). Last year too, we were one of the finalists. <br>
<br>
How much has the out-of-the-box thinking helped my company in the nomination to 
the Awards? When this company started in 1999, the biggest challenge was that it 
had a very insignificant market in Oman. So we started eyeing the market outside 
Oman. There were opportunities outside Oman waiting to be tapped. Today, that is 
the strength of the company. We are now exporting our products to 26 countries, 
including the US, UK, Germany, Belgium, Luxemburg, Netherlands, Canada, New 
Zealand, Australia etc. We have grown as a team because we have always believed 
in out-of-the-box thinking. We started looking beyond our noses. We started 
looking beyond Oman. <br>
<br>
But it’s not been easy. We are not a very large company. We do not operate out 
of a very large country, where there is very strong industry. But steel is a 
very strong industry, where 90 per cent of the requirements are staggered 
throughout the globe.<br>
<b><br>
Formula for Success</b><br>
<br>
We are a very small player but we have been able to make inroads into markets 
like the US. Now, this is a big achievement. This has happened because of two 
factors: sustained quality and competent delivery schedule. We are the first 
company to sell pipes in containers. That, again, is an example of 
out-of-the-box thinking. We are able to grow to this level because we are able 
to sell pipes in containers.<br>
<br>
How did this idea of selling pipes in containers come about? Well, it was based 
on proper studies of the market. Now, it is very difficult to believe that pipes 
can be exported in containers. But we did our homework well and we sold this 
idea to our customers quite successfully. Out-of-the-box thinking, you see!<br>
<br>
<b>Management Style</b><br>
<br>
Has there been any perceptible change in the style of management at my company 
since I joined? Well, one of the biggest changes for me was to build a team that 
performs. I believe in getting results through the people, through the team. You 
have to have the right team. And you have to empower your team, and give them 
the right kind of responsibility and authority. I believe then only can you get 
the things done. <br>
<br>
I tend to focus on my company’s core business. That’s my style of management.<br>
<br>
To give you an example, this company had a total strength of 500 employees 
initially. Now, it stands at 300. We found that certain functions could be 
outsourced and we looked for a model. Functions like packaging, despatches, 
which are voluminous in nature and highly labour-oriented, were affecting our 
operations considerably. So we identified certain key areas for outsourcing, 
like packaging and transportation. <br>
<br>
Today, we able to produce and sell 13,000 tonnes of steel a month with just 300 
people. The outsourcing model has worked wonders for the company, both 
administratively and operationally. <br>
<br>
However, success depends on how well you manage the outsourcing model. Just 
resorting to the outsourcing model is not enough. You have to manage it well. 
You need to have a good team of management to manage the outsourcing model.— As told to Mehre Alam</font></p>

