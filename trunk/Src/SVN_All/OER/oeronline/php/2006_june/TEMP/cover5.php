            <img border="0" src="almahalogo.jpg" width="112" height="74" align="left" hspace="5" vspace="2"><br>
            RIDING on Retail</b></font><font face="Verdana" size="1"><b><br>
            Spiraling crude oil prices have kept Al Maha Petroleum Products 
            Marketing company on a tight leash, pushing it hard to enlarge its 
            reach</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1">l 
            Maha Petroleum Products Marketing SAOG once again occupies the high 
            table in 2005 among the Top 10 corporates in the Sultanate. It is 
            there, perched at Number 5. Moving a few notches up and down is all 
            part of the game. Last year, Al Maha was in the top league, 
            occupying the No. 3 slot. The advent of Omantel to covet the No.1 
            slot, displacing Shell Oman, has upset the applecart to an extent.<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="almaha1.jpg" width="200" height="260" align="left" hspace="5" vspace="2"></b></font><font face="Verdana" size="1"><b>Challenges</b><br>
            The primary challenges that Al Maha identified and handled deftly 
            were net profit, competition, customer service, fixed profits 
            margins and environmental issues. “It is our responsibility to take 
            necessary measures to deal with these challenges in order to achieve 
            our objectives and move forward,” said Chairman Rashed Saif Al 
            Suwaidi while addressing the Annual General Meeting in mid February 
            this year. <br>
            <br>
            With the unrelenting rise in the price of crude oil since May last 
            year keeping Al Maha on a tight leash, the 120-retail outfits 
            petroleum product marketing company pushed hard to enlarge its 
            reach. More filling stations literally meant higher fuel sale and 
            increased market share in the retail sector. When curtains came down 
            on December 31, 2006, Al Maha had added nine brand new filling 
            stations besides acquiring a solitary petrol pump from one of its 
            competitors. <br>
            <b>
            <br>
            Bottom line </b><br>
            Focused approach did yield rich dividends for Al Maha. 
            Improving the bottom line through optimum utilization of available 
            resources was the only sane choice it had. “We are vigorously 
            driving our resources towards cost control, enhanced efficiency and 
            higher quality,” says Suwaidi. No wonder that 2005 witnessed a 22 
            per cent jump in turnover, 27 per cent rise in profit after tax and 
            40 per cent escalation in earnings per share. Did it delight the 
            shareholders? You bet it did with a 35 per cent dividend, the 
            highest ever since it came into existence as a separate corporate 
            entity way back in 2000. Despite the oil sector being viewed as 
            defensive, investors saw the share price doubling in 2005: from RO 4 
            to RO 8. Analysts, however, are cautious. Comments Fincorp: “We will 
            have to see whether the company keeps up the pace of expansion as we 
            have seen in 2005.”<br>
            <br>
            <b>Business</b><br>
            A close look at the financials reveals that the retail sales 
            delivered a significant 27 per cent growth in 2005 vis-à-vis the 
            previous year. It was a volume game coupled with the rise in the 
            selling price in mid-2005. As a strategy, like other oil marketing 
            corporates in Oman, Al Maha is also adding to its “Souk” outlets in 
            filling stations, which stands at a grand 50. Bulk sales shot up by 
            12 per cent last year, purely driven by an increase in selling 
            price. Al Maha witnessed a 13 per cent upsurge in aviation fuel sale 
            in 2005. <br>
            <b><br>
            Best Practices</b><br>
            KPMG, the statutory auditors to Al Maha, have given a clean chit on 
            the implementation of Code of Corporate Governance as per the norms 
            laid down by the Capital Market Authority in the Sultanate. Besides, 
            the company is focused on ensuring competence, behaviour-based 
            programmes, commitment at all levels and managing change as far as 
            health, safety and environment commitments are concerned. While Al 
            Maha has effortlessly notched up a 85 per cent Omanisation target, 
            the company’s HR polices, admits Chairman Suwaidi, seek to identify, 
            develop and reward employees “who demonstrate the qualities of 
            individual initiative, drive, determination, hard word and loyalty 
            in his/her job”. Nevertheless, a lot more is currently underway. A 
            considerable amount of work on the business process and business 
            re-engineering study, including the review of manuals to review 
            efficiency and to ensure full compliant with the Code of Corporate 
            Governance, has been completed.<br>
            <br>
            The year 2005 had a lot of challenges that impacted overall HSE 
            performance. As compared to previous years, 2005 was a busy year by 
            any standard. The HSE Management system was completely revised to 
            suit to the present organization set up. New manual was prepared. 
            Along with this, there were continuous efforts to drive HSE to reach 
            the hearts and minds of every one involved in Al Maha and its 
            contractors operations.<br>
            <br>
            <b>Forecast</b><br>
            Al Maha anticipates that the retail sales will go up by 16 per cent 
            in 2006. Adding more “Souks” is a certainty given the fact that 
            hardly 50 out of 120 filling stations sport such a facility. 
            Chairman Suwaidi does not mince words and admits that bulk sales 
            will remain flat in 2006. However, he forecasts a 10 per cent rise 
            in aviation fuel sales this year. Both analysts and the company are 
            confident that in 2006, Al Maha will once again cross the RO 
            100-million mark. What about the investors? Reasonable cash dividend 
            is what Chairman Suwaidi promises in the early days. Is he playing 
            safe by under-promising, but over-delivering? One wishes, he does.</font></p>
