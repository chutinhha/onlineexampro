            <img border="0" src="oiblogo.jpg" width="312" height="57" hspace="5" vspace="2"><br>
            <br>
            STEADY PROGRESS</b></font><font face="Verdana" size="1"><b><br>
            OIB has been pushed to the 10th position in the OER Top 20 ranking, 
            down by two places compared to 2004, despite making a steady 
            progress on the revenue and net profit front</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="oib1.jpg" width="250" height="262" align="left" hspace="5" vspace="2">W</b></font><font face="Verdana" size="1">ith 
            the banking industry doing so well in Oman during 2005, Oman 
            International Bank (OIB) excelled in consolidating its financial 
            performance. It strengthened its position in the local market 
            through enhancing asset quality, controlling operating expenses, 
            while improving customer service. <br>
            <br>
            Launched in 1984, OIB is the first 100 per cent Omani owned 
            commercial bank in the Sultanate. Currently, it has a network of 82 
            branches in the country. It has four overseas branches — in Mumbai 
            and Cochi in India and Karachi and Lahore in Pakistan.<br>
            <br>
            Reem Omar Zawawi, Chairperson of OIB says, “Business continues to 
            perform well, driven by a strong momentum in retail, corporate and 
            treasury activities.” As a result of a strong growth in loan book 
            and improved spreads, the net interest income rose from RO 27.493 
            million in 2004 to RO 32.512 million in 2005, growing at the rate of 
            18.3 per cent. However, the other income of RO 8.637 million for 
            2005 increased marginally by 1.5 per cent.<br>
            <br>
            “Our efforts to control operating expenses continue, with several 
            cost rationalisation initiatives undertaken during the year. As a 
            result, operating expenses increased marginally by 0.9 per cent in 
            spite of a strong growth in income,” informs Zawawi. The total 
            operating expenditure, which includes staff cost, premises, 
            depreciation and other administrative expenses, increased marginally 
            from RO 17.439 million in 2004 to RO 17.601 million in 2005, despite 
            strong increase of 14.3 per cent in the total income. The cost to 
            income ratio improved to 42.8 per cent in 2005 compared to 48.4 per 
            cent in 2004. Accordingly, the bank’s operating profit increased 
            from RO 18.561 million in 2004 to RO 23.548 million in 2005, 
            resulting in an outstanding growth of 26.9 per cent. <br>
            <br>
            The bank has maintained its strategy of improving asset quality and 
            reducing the provision for loan impairment. The specific provision 
            for loan impairment for 2005 decreased by 20.2 per cent to RO 3.520 
            million. However, the general loan loss provision increased from RO 
            2.191 million in 2004 to RO 2.786 million in 2005 — in line with the 
            requirements of the Central Bank of Oman. <br>
            <b><br>
            Shareholder’s wealth </b><br>
            On the recovery of non-performing loans, the bank made an excellent 
            progress by recovering RO 7.688 million compared to RO 3.273 million 
            collected in 2004. As a result of these measures, the bank further 
            improved on its excellent performance on the net profit front in 
            2004. The bank achieved a net profit of RO 22.028 million in 2005, 
            indicating a high growth of 64.7 per cent over the previous year. 
            Buoyed by the results, the bank proposed a cash dividend of 23 per 
            cent and a stock dividend of 20 per cent for distribution in 2006.
            <br>
            <br>
            “Overall, 2005 was a rewarding year for the bank’s 
            shareholders. The earnings per share improved from RO 0.213 in 2004 
            to RO 0.350 in 2005. In addition to the payment of the cash dividend 
            of 21 per cent on the face value of the share, the bank’s share 
            price appreciated by 31 per cent,” says Zawawi. <br>
            <br>
            The loans and advances stood at RO 510.487 million as on 31 
            December, 2005 compared to RO 454.818 million as on 31 December 
            2004, representing a growth of 12.2 per cent year on year. Both 
            retail and corporate loans showed strong growth during the year. The 
            total deposits stood at RO 655.373 million as on 31 December 2005 
            compared to RO 571.369 million as on 31 December 2004, representing 
            a growth of 14.7 per cent year on year, which is lower than the 
            industry average. In comparison, on an average, the deposits grew by 
            22.1 per cent in the Omani banking industry. <br>
            The bank continued with its strategy of improving the loan loss 
            coverage ratio. The ratio improved significantly from 91.2 per cent 
            as on 31 December 2004 to 98.8 per cent as on 31 December 2005. The 
            coverage ratio increases to 107 per cent after considering the value 
            of the underlying securities.<br>
            <b><br>
            Overseas operations</b><br>
            Internationally, especially in India, OIB’s strategy of focusing on 
            recovery of problem loans, limiting asset growth, improving cost of 
            funds and reducing operating costs continued in 2005. According to 
            Zawawi, “The results continued to be encouraging with a strong 
            improvement in the operating income. We have also seen positive 
            developments in recovering some non-performing loans. The operations 
            in Pakistan continue to be closely managed. We are currently 
            exploring various options to meet the increased capital requirements 
            imposed by the State Bank of Pakistan.”<br>
            <br>
            The bank has been progressing well on the Omanization front. It won 
            the GCC localisation award for the employment of Omanis. At the 
            year-end, its Omanisation level of 92.42 per cent was higher than 
            the Central Bank of Oman stipulations. The total balance sheet of 
            the bank stood at RO 820.835 million as of 31 December 2005 compared 
            to RO 717.823 million as of 31 December 2004. <br>
            <br>
            The outlook for 2006 looks positive for the bank. As recommended by 
            Fincorp, OIB scrip has good chances for going up on MSM.</font></p>
