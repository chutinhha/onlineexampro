            </font><font face="Verdana" style="font-size: 16pt">DEBUT at the Top</font></b><font face="Verdana" size="1"><b><br>
            Omantel enters the OER Top 20 rankings in style, right at the top, 
            in its very first year of getting listed on the MSM. It has clinched 
            the crown from Shell Oman Marketing Co., which had been occupying 
            the top slot for the last four years</b><br>
            <br>
            </font>
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">2</font><font face="Verdana" size="1">005 
            was, arguably, the most eventful year in the history of Oman 
            Telecommunications Company SAOG (Omantel). Two major developments 
            that changed the telecom landscape in Oman had a major bearing on 
            Omantel. The first private telecom player — in the mobile segment — 
            made its entry as the second telecom company in the Sultanate. Then 
            came the Initial Public Offering (IPO) of Omantel. On 28 July 2005, 
            Omantel shares were listed on the Muscat Securities Market (MSM).
            <br>
            <b><br>
            <img border="0" src="omantel.jpg" width="200" height="262" align="left" hspace="5" vspace="2">Telecom 
            Landscape</b><br>
            The government has been following the liberalisation policy in the 
            telecom sector in line with the World Trade Organisation (WTO) 
            agreement. The introduction of a Telecom Act and establishment of 
            Telecommunications Regulatory Authority (TRA) is a step in that 
            direction. Currently, Omantel has 70 per cent ownership by the 
            government and 30 per cent by the private investors.<br>
            <br>
            Beginning March 2005, the market was thrown open with the entry of 
            the second telecom company in the mobile space. TRA intends to 
            further liberalise the market by issuing new licences related to 
            fixed telephony, public data and value added services. This will 
            transform Oman into a multi-player, vibrant and competitive market.
            <br>
            <br>
            It requires Omantel to adopt a flexible and lean operating model 
            with articulated business strategies along with competent workforce, 
            all sharing a commercial and business driven culture. Along this 
            line, the management of Omantel has adopted a strategy to transform 
            itself into a profit-oriented, competent and aggressive telecom 
            player, which also takes care of its social responsibilities. <br>
            <b><br>
            Financial Performance</b><br>
            Omantel’s top line increased significantly in 2005. The total 
            service revenues (comprising revenues from fixed line, Internet and 
            data, mobile services and in-payment and inter-connection services) 
            increased from RO 247.9 million in 2004 to RO 270.6 million in 2005. 
            This indicates a growth of 9.4 per cent. The total revenue is higher 
            by 1.3 per cent compared to the IPO prospectus projection of RO 
            267.1 million. <br>
            <br>
            The growth in total service revenues in the 2003-2005 period has 
            been mainly driven by the increase in mobile revenue, resulting from 
            an increase in the number of mobile — particularly prepaid — 
            subscribers. Fixed line revenues fell during the same period. 
            Aggregate Internet and data communications revenue gradually rose 
            during the same period. <br>
            <br>
            The increase in other revenue for the year 2005 is mainly due to 
            interconnection service provided to new mobile operator. (To comply 
            with TRA guidelines, the roaming expense, RO 4.5 million in 2005 and 
            RO 2.43 million in 2004, is removed from the total service revenue 
            and shown separately under operational expenses).<br>
            <br>
            The operating expenses increased by 15.4 per cent to RO 193.8 
            million in 2005 compared to RO 167.9 million in 2004. This has also 
            impacted the net profits of the company. “The increase in operating 
            expenditure is due to increase in international administration 
            expenses, depreciation, marketing and operating &amp; maintenance 
            expenses. The increase in operating expenses is to enhance the 
            network coverage and to comply with the quality of service 
            parameters that are required in a competitive scenario, ” defends 
            Saud Bin Nasser Al-Shaikili, Board Chairman, Omantel.<br>
            <br>
            Fixed line services: As reflected in the table (Segment Wise 
            Revenues — 2003-2005), the revenues from the fixed line services 
            (including national and international fixed line voice, prepaid 
            cards-Jibreen and payphones-Al Multaqa) have gone down in the 
            2003-2005 period. The average number of subscribers (including 
            pre-paid and payphone services) has gone up marginally (6 per cent) 
            from 235,037 in 2003 to 256,106 in 2005. The blended monthly average 
            revenue per user (ARPU) has declined from RO 28.5 in 2003 to RO 
            21.5.<br>
            <br>
            Internet and data services: The Internet and data communications 
            services of Omantel includes Internet dial-up, broadband services 
            (ADSL “PowerNet”), prepaid services (“Al Ufuq”) and Internet 
            dedicated services. It also includes leased circuits (domestic and 
            international services). <br>
            <br>
            Internet revenues have been on an upswing, going up from RO 6.7 
            million in 2003 to RO 10.7 million in 2005. The average Internet 
            subscribers, including broadband, have also gone up from 52,726 in 
            2003 to 72,689 in 2005. The average revenue per subscriber 
            year-on-year increased by 4.6 per cent in 2004 and by 11.5 per cent 
            in 2005. The average revenue per subscriber per month blended was RO 
            10.6 in 2003, which increased to RO 11.03 in 2004. In 2005, it was 
            RO 12.3. <br>
            <br>
            The major factor for the increase in APRU is the addition of 
            hi-speed broadband service (ADSL). In 2006, the Internet-based 
            revenues are expected to climb up further with Omantel recently 
            launching WiFi Wireless Internet service ‘Ibhar’. <br>
            Mobile services: The mobile services business of Oman Mobile, part 
            of Omantel Group, has registered a spectacular growth in 2005 
            despite the opening up of the mobile telecommunications sector. In 
            July 2005, the total subscriber base reached the one million mark. 
            In comparison, the average subscriber base was 528,657 in 2003. It 
            went up to 699,867 in 2004. As on April 2006, it stands close to 1.2 
            million. <br>
            <br>
            Talking about Oman Mobile’s good performance, Dr. Amer Bin Awadh Al 
            Rawas, Managing Director, Oman Mobile says, “Oman Mobile has been 
            able to double the number of subscribers through a number of 
            transformation steps that we took to change the company from a 
            government-owned utility public authority to a consumer and 
            market-oriented private sector company. This has resulted in an 
            increased interest from the community in our services. In 2005, we 
            came out with a large portfolio of services and achieved a full year 
            of successful production, launching over 25 products and services 
            that cater to every segment of the market in one year. We were, and 
            still are, ranked the largest telecommunication distribution channel 
            in Oman with over 11,000 points of sales by the end of 2005.”<br>
            <br>
            As a result of the fast track growth of the mobile business, Omantel 
            Group’s mobile revenues have overtaken that of fixed line revenues. 
            In 2005, mobile revenues were RO 163 million, accounting for 60 per 
            cent of the total revenues of the group. In comparison, the mobile 
            revenues were RO 110.2 million in 2003 and RO 146 million in 2004. 
            Evidently, the mobile segment has been the primary growth driver of 
            the Omantel Group’s revenues over the last three years, particularly 
            after the introduction of the prepaid services in 2001.<br>
            <br>
            However, the increase in the number of subscribers and competition, 
            leading to price cuts, has affected the blended mobile APRU as it 
            has decreased from RO 17.4 in 2003 to RO 14.3 in 2005.</font></p>
