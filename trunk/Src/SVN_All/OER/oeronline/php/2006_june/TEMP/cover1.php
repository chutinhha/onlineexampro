            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">Omantel committed over RO 1 million to provide backbone telecom 
            infrastructure in Sohar Port. It is also working towards striking a 
            partnership with port authorities to establish management system in 
            the port in order to streamline the shipping process and customs 
            through a fully automated system. <br>
            <br>
            It has set up a joint venture, Infoline LLC, which is engaged in the 
            provision and management of call center services. Omantel has also 
            invested over RO 15 million in the FLAG (FALCON), which is in the 
            advanced stages of building a new resilient sub sea fibre optic loop 
            cable system. The system has two landing stations in Oman (Seeb and 
            Khasab). This project, once completed, will serve as the traffic hub 
            for carrying traffic across the Middle East and the Indian 
            subcontinent. It also signed a three-year agreement with Eutelsat 
            for the lease of a full transponder. <br>
            <br>
            All this, besides a host of other investments and programmes 
            undertaken by Omantel, is aimed at strengthening the infrastructure 
            to service not only the existing customer requirements but also for 
            building up the capability to meet the growing service demands. 
            These long-term investments must have impacted the bottom lines of 
            the group in the short run.<br>
            <b><br>
            <img border="1" src="omantel1.jpg" align="left" hspace="5" vspace="2">Opportunities 
            Galore </b><br>
            “We are optimistic and upbeat that our past efforts along with 
            organization-wide adoption of customer-focused business strategies 
            and processes would bring enormous success in the years to come and 
            we would be able to take the company to new heights,” exudes Saud.<br>
            <br>
            Already, the first quarter results of 2006 have been quite 
            encouraging. The net profit has jumped by 9.6 per cent. As per the 
            un-audited figures released by the group, the net profit shot up 
            from RO 19.528 million in Q1 2005 to RO 21.408 million in Q1 2006. 
            The revenues also went up by 22.6 per cent from RO 63.578 million in 
            Q1 2005 to Ro 77.966 million in Q1 2006.<br>
            <br>
            Omantel has started the year 2006 on a positive note. Its bid for 
            the third mobile license in Yemen is close to success as the company 
            has been invited by the Yemeni authorities to work out the 
            modalities for the operations. It is a tremendous boost to the 
            company’s overseas expansion plans. <br>
            <br>
            Omantel also won a contract with Petroleum Development Oman (PDO) to 
            build the infrastructure for the transmission network of PDO and for 
            linking its strategic sites through fibre optic cables on the one 
            hand, and Synchronous Digital Hierarchy (SDH) equipment as well as 
            Dense Wavelength Division Multiplexing (DWDM) systems on the other 
            hand. The contract’s value is RO 12.573 million. <br>
            <br>
            Omantel unveiled its new MPLS service at the recently held Comex 
            2006 Exhibition. It is a virtual private network (VPN) that is 
            provided through the Internet Protocol, which combines any flexible 
            contact over the telephone or the Internet in an effective and high 
            quality manner, safely delivering it through lease line services. 
            MPLS provides several levels of performance and has the ability of 
            delaying the flow of data and information. It also enables voice and 
            multimedia applications — all these facilities shall be available in 
            one network. <br>
            <br>
            Omantel has also recently launched the hi-speed wireless Internet 
            service (Wi-Fi) ‘Ibhar’. Dr. Mohammed Bin Ali Al-Wohaibi, Executive 
            President, Omantel informs, “The new service aims at attracting 
            large segment of laptop and mobile notebook users. It helps 
            subscribers visit the Internet while moving from one place to 
            another through hotspots made available in more than 12 locations in 
            public places. Thanks to these hot points, Wi-Fi users can easily 
            finish their jobs without the mess of wires.” Recently, Omantel also 
            launched WLL service project to connect the Sultanate’s rural 
            villages. This project will cover around 200 rural villages. <br>
            <br>
            Despite the impending threat from competition, Omantel believes 
            there still are opportunities to increase the customer base and 
            penetration levels in Oman. As the leading telecommunication service 
            provider, Omantel is strongly placed to compete with newcomers in 
            mobile as well as fixed segments. It enjoys the advantage of strong 
            national brands, fully built-out and up to date network, capable 
            workforce, national distribution as well as systems and processes in 
            place to provide and deliver services and support to the customers.
            <br>
            <br>
            While the company does accept the fact that competition will lead to 
            an erosion of market share in Oman, it also expects it to stimulate 
            growth of market. With the opening of the mobile market, Oman Mobile 
            witnessed an exponential growth in subscribers as well as revenues.
            <br>
            <br>
            In addition, liberalisation provides Omantel with opportunities to 
            grow internationally in markets, which have also opened up for 
            competition. The invitation by Yemen to Omantel to run mobile 
            services there is a good example of this. The competition also 
            provides Omantel with the opportunity to position itself as carrier 
            of carriers on the basis of its unique network position, thus 
            utilising the evolving competition to further business 
            opportunities. <br>
            <br>
            As per Omantel management’s statement in its Annual Report for year 
            2005, “The expected competition in the fixed line business may turn 
            into a serious threat if it is not properly implemented. Omantel 
            takes the view that competition is beneficial but that it needs to 
            be implemented in the right market at the right time and under the 
            right terms and conditions. While we are not certain how the 
            liberalisation policy would evolve, we are confident that TRA’s 
            sector policy would provide a balanced path to liberalisation 
            programmes based on the currently adopted way of consulting market 
            players before finalising the policy.”</font></p>
