            <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"><b>Major 
            Developments in 2005</b><br>
            In order to achieve significant growth in revenues and subscribers, 
            Omantel launched a series of initiatives in 2005. It introduced over 
            20 value-added mobile services. It took the initiative to introduce 
            managed network services to provide effective and efficient telecom 
            solutions to the government, banks and other major corporate 
            clients. It launched attractive and value-for-money consumer 
            promotions in mobile, fixed and Internet services. It also extended 
            customer service counters/distribution network with enhanced service 
            standards. <br>
            <br>
            In 2005, the company signed over RO 78 million worth of contracts, 
            mostly infrastructure-based expansion, covering both Omantel and 
            Oman Mobile. Over RO 51 million worth of contracts was signed for 
            Oman Mobile network expansion to provide seamless coverage to most 
            of the populated areas and major highways/roads. In the last 
            five-year plan (2001-2005) period, Omantel has invested RO 383 
            million in telecom infrastructure development.<br>
            <br>
            Another notable development of 2005 was the creation of Tejari Oman. 
            Omania E-Commerce, jointly promoted by Omantel, entered into a 
            partnership with Tejari, the Middle East premier online 
            business-to-business (B2B) marketplace. This new initiative will 
            enable both government departments and private sector organisations 
            to source goods and services through online trading hub, enhancing 
            transparency and lowering the costs associated with paper-based 
            procurement processes.</font></p>
