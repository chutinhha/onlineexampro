                <font face="Verdana" size="1"><b>Stock Performance</b><br>
                As part of the privatisation process, a portion of the share 
                capital was divested to public at a price of RO 1.28 in June 
                2005. In its first year as a listed company, Omantel’s net 
                profit for the year 2005 stood at RO 68.15 million as against RO 
                72.95 million compared to 2004, a decrease of 6.6 per cent on a 
                year on year (YoY) basis. This, in spite of the stability seen 
                in its core revenue! <br>
                <br>
                On the other hand, a positive sign emanating from the financials 
                is the improvement in its other operating revenues whose main 
                contributor has been the interconnection revenues and external 
                administration revenues. <br>
                <br>
                Overall, the annualised earnings slipped from 97 baizas to 91 
                baizas on year to year basis. The stock is getting traded at P/E 
                multiple of 14.15, which is lower than the GCC telecom average 
                of 21. The share is worth RO 1.82 if we apply the P/E of 20 
                (slightly less than the GCC average of 21) to the 2005 earnings.
                <img border="0" src="dailypricechart.jpg" width="250" height="180" align="right" hspace="5" vspace="2"><br>
                <br>
                However, there also are issues that require a major attention in 
                the financials. Royalty payment to the government is to be based 
                on net revenue, which was allowed by the government for the year 
                2004, in line with the international practice. However, the 
                company continues to make provisions based on gross revenue and 
                this could create a substantial impact (positive) on the bottom 
                line. The company has been reporting certain amounts under the 
                head “Items Awaiting Capitalization” for the past four years (RO 
                98 million). The company needs to clarify when the 
                capitalization will be carried out.<br>
