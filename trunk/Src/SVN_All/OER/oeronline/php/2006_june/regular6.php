                <b>Challenger to Amazon <br>
                </b><br>
                <img border="1" src="worldscan_2.jpg" align="right" width="100" height="96">When 
                we talk of online retailing, Amazon’s name comes first to our 
                mind. But Amazon now has a serious competition with the growing 
                popularity of Otto. With sales of US$3.8 billion in 2005, Otto, 
                the German company, is now the world’s second-biggest online 
                retailer. The company has its roots in the mid-1990s when its 
                founder Michael Otto was part of an experimental project of Time 
                Warner to test the willingness of the residents of Orlando (USA) 
                to shop online, using a specially installed cable network. 
                Moving on, he acquired Crate &amp; Barrel in 1998 and converted it 
                into one of the fastest-growing retailers in the US with focus 
                on catalog and online sales. The Otto group notched up sales of 
                US $18.5 billion in 2005 and has 54,000 employees globally. Its 
                online sales portfolio includes all kinds of products such as 
                garments, sports goods, electronics and appliances. Otto Group’s 
                core business is still the German catalog, but it generates half 
                of its sales outside of Germany, including other European 
                countries, the US and parts of Asia. Michael Otto continues to 
                push into new areas of e-commerce. His current endeavour is a 
                “t-commerce” partnership with Microsoft that would allow 
                customers to buy from the Otto catalog using a TV, a remote 
                control, and a set-top box. Otto hopes to reach the millions of 
                people still not comfortable with the Internet through this. </font></p>
