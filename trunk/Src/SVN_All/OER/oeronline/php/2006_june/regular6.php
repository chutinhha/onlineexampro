                <b>i-Pod-enabled mobile<br>
                <br>
                <img border="1" src="worldscan_1.jpg" align="left" width="71" height="122"></b>According 
                to media reports in Japan, Apple is expected to introduce an 
                iPod-enabled cell phone. The new phone, which Apple is 
                developing with Japanese company Softbank, will be introduced 
                later this year in Japan. Softbank recently purchased Vodaphone 
                Japan, and will continue to develop next generation phones. 
                Softbank’s President, Masayoshi Son, was reported to have 
                recently met with Apple’s CEO, Steve Jobs, to chalk out a basic 
                agreement on how an Apple iPhone would be developed. Currently, 
                Japan uses its own priority cellular network and if one were to 
                purchase an Apple iPhone in Japan with the intention to use it 
                in North America, it simply wouldn’t work. As per reports, Apple 
                was developing plans internally to launch its own cell phones. 
                Apple already has a partnership with Motorola for integrating 
                iTunes into phones.</font></p>
