                <b>Carrefour in Iran<br>
                <br>
                <img border="0" src="worldscan_4.jpg" align="left" width="150" height="33"></b>Notwithstanding 
                the strained relationship between Iran and the US, the leading 
                French hypermarket chain, Carrefour, is planning to make an 
                entry into the Iran market. As per MAF LLC, Carrefour’s regional 
                partner, an entry in Iran is among the company’s business 
                expansion plans. However, nothing has been finalised as of now. 
                Market analysts say Carrefour is expected to start up to 14 
                stores in Iran in the next 5-6 years. The first store could come 
                up as early as in 2007 in Tehran. Iran’s retail market doesn’t 
                boast of many hypermarkets, something quite common in the GCC 
                market.<br>
                <br>
                <b>Philips mired in media blues<br>
                </b><br>
                <img border="1" src="worldscan_3.jpg" align="right" width="150" height="105">Philips, 
                sponsors of FIFA World Cup 2006, got caught in a media row 
                recently. The company came in for heavy flak for offering an all 
                expense paid trip of Germany to watch the World Cup to 
                journalists in the Middle East in return of a “favourable 
                coverage” in their respective publications. The company sent 
                letters to a select group of journalists in the region, stating 
                that the one who gives the best coverage to Philips in the 
                period leading to the event will win the trip. The offer sparked 
                off a major controversy and a debate raged on the issue of 
                bribing journalists indirectly to get the media coverage. To 
                clear its stand, Philips issued a statement: “The issued Philips 
                letter to the media was merely part of our efforts to engage 
                with some targeted media and not an offer for coverage. If the 
                wordings of the letter led to some misrepresentation then we 
                sincerely apologize.”<br>
