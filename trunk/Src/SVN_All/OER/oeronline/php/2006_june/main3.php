<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" style="font-size: 14pt">
<br>
</font><font face="Verdana" size="5">Oman-Egypt tourism 
venture to roll out soon</font><font face="Verdana" style="font-size: 16pt"><br>
</font></b><font face="Verdana" size="1"><b><br>
The tourism sector in Oman is set to add a new property, with the announcement 
of Muriya project, an Oman-Egypt joint venture </b><br>
<br>
</font><b><font face="Verdana" style="font-size: 11pt">
<img border="1" src="inthenews.jpg" align="right" width="150" height="85">T</font></b><font face="Verdana" size="1">he 
Government-owned Oman Tourism Development Company (OTDC) and Egypt’s Orascom 
have unveiled the master plan for the prestig</font><font face="Verdana" size="1">ious US$600 million joint venture — Muriya. On behalf of the Oman Government, OTDC has picked up a 30 per cent stake 
in the new tourism venture, and Egypt’s Orascom picking 70 per cent. Besides 
Oman, Orascom has a large number of ambitious projects lined up in Egypt, Jordan 
and UAE, Switzerland and Mauritius.<br>
<br>
“We are committed to fully exploiting the Sultanate’s tourism potential, which 
will help stimulate long-term economic development, as Oman has much to offer to 
the tourists in landscape, climate, and water sport along with a politically 
stable and safe location,” said HE Dr. Rajiha bint Abdul Ameer bin Ali, Minister 
for Tourism. Oman is rapidly developing as a prime destination of natural 
splendor and authentic Arabian heritage and hospitality. Oman is also pressing 
ahead with plans to further develop its eco-tourism and natural attractions.<br>
<br>
The mild, but sunny winter climate is attractive to those looking to escape from 
the rigours of the northern winter. The cooler monsoon climate of the south, 
like in Salalah, also offers relief from the searing heat.<br>
<br>
Dr. Rajiha, who is also the chairperson of OTDC, feels confident about the 
project, which is sure to boost the domestic tourism industry, and at the same 
time, spawning more job opportunities for nationals. Taking many a initiative, 
the Sultanate seems to going meticulously about meeting its objecitve of giving 
greater fillip to inbound tourism. It is the government’s intention to expand 
tourism as rapidly possible so that it becomes a major factor in the economy of 
the country. Muriya proposes to transform Qurum into a classy township by 
upgrading its status as the hub of the city, involving an elegant 250-room six 
star hotel, an Omani souq linked to a 35,000 sq meters shopping mall, a 35,000 
sq meters office building to house the most reputed local and foreign companies, 
and a retail pedestrian street — promenade — packed with exclusive brand 
outlets, cafes and restaurants, a six -screen cinema theatre, and underground 
parking garage, to name a few. There will be two bridges crossing the Wadi.<br>
<br>
The government has proceeded cautiously to ensure that the tourism development 
is pursued in a well- planned manner. Tourism today is one of the largest and 
most vital sectors of the global economy, driving vast flows of people, 
commodities and capital. It literally operates on the leading edge of 
globalization as it continually transfers consumer tastes, cultural practices, 
business people and capital into new space across the globe. A primary goal of 
tourism development should be fostering uniqueness - sense of place rooted in 
local cultural, historical and environmental features.<br>
<br>
Oman has a great potential to developing nature and culture based tourism, 
usually organised through small group tours. In addition to developing culture 
attractions, such as remodeled forts, the key also lies in developing some 
luxury resort complexes. Through Muriya, it plans to develop four hotels - 4-6 
stars in Salalah as well as in Sifah. It will encompass inland marina and marina 
towns, comprising luxurious real estate apartments. An 18-hole golf course and 
water sports, in addition to tracks for horse and camel riding will also be 
urbanised. Meanwhile, in the Al Soda Island, a luxurious boutique resort would 
be developed, with seven star amenities.</font></p>

