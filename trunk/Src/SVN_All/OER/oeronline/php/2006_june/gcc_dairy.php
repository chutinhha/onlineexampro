<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b><img border="0" src="gcc_dairy_2.jpg" width="300" height="54"></b><br>
Kuwait plans to privatise non-core oil sector in a phased manner by 2010. “This 
plan aims to allow Kuwait Petroleum Corporation (KPC) to focus on exploration 
and production of oil and natural gas,” Energy Minister Sheikh Ahmad Fahd al-Sabah 
said. This so far remains under state control. <br>
<br>
By the end of the current year, KPC plans to sell at least 30 per cent of its 
foreign arm, the Kuwait Foreign Petroleum Exploration Company (KUFPEC), and 49 
per cent of Kuwait Drilling Company. Kuwait has already started privatising the 
oil-marketing sector by signing agreements with Al-Oula and Al-Sour companies. 
The minister expressed confidence that private participation in the oil sector 
would allow government to divert its resources in developing other areas of oil 
industry where private participation is not allowed. <br>
<br>
The country is planning to invest over US$20 billion in next five years for the 
development of the oil industry. Kuwait is also planning to privatise two 
petrochemicals projects producing propylene and fertilizers, and KPC subsidiary 
Kuwait Oil Tanker Company (KOTC), which currently operates 24 oil tankers for 
crude and petroleum products. This liberalisation strategy will also include the 
granting of licences for up to three new oil tanker companies.<br>
<br>
Kuwait also plans to privatise at least 25 per cent of the planned new 615,000 
barrels per day refinery, which is estimated to cost US$6.3 billion and slated 
to come on line in 2010. The minister said KPC planned to sell its butane gas 
factory, the only one of its kind in Kuwait, and a hospital run by the oil 
sector. Meanwhile, a parliamentary committee unanimously approved a draft 
privatisation law. It is, however, yet to be debated in the National Assembly. 
The bill facilitates the sale of electricity, communications and education 
services but bans the privatisation of oil and gas production. The bill also 
allows the government to retain a golden share of the privatised companies, 
which would give it veto power on any board of directors.</font></p>
