<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b><img border="0" src="gcc_dairy_1.jpg" width="309" height="51"></b><br>
The Kingdom of Saudi Arabia plans to privatise its stock market and establish 
the Middle East’s largest financial district as a part of economic reforms to 
boost investments. The financial districts that will be constructed over an area 
of 3 million sq. meters are going to house stock exchange as well as banks, 
brokerage services and other financial institutions. This project is expected to 
be completed in three years.<br>
<br>
The move to privatise the bourse will help the stock market operate on more 
professional and transparent bases and introduce new products and services, 
analysts expect. The Saudi stock exchange that operates on an inter-bank basis 
is run by Tadawul, which is managed by the board of the Capital Market Authority 
(CMA), a government organisation. The CMA is looking to launch the Saudi Arabian 
Financial Exchange [SAFX], a private sector corporation. Though the time frame 
is not set for converting the stock exchange as a joint stock company, the 
new-look exchange will introduce new products and services as per the needs of 
the investors.<br>
<br>
The Saudi government may transfer management of the stock exchange to the new 
holding company in next few months, following which the exchange will float 
initial public offering (IPO). The value of the stake to be offered has not yet 
been decided. However, the Saudi government usually offers stakes of around 30 
per cent in its companies.<br>
—Sowya Sri Rallabhandi</font></p>

