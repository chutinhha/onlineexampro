<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b><img border="0" src="gcc_dairy_3.jpg" width="304" height="64"></b><br>
Doha Asian Games Organising Committee has launched the much-awaited sales of the 
15th Asian Games Doha 2006 tickets. DAGOC has released over 500,000 out of the 
1.4 million tickets for the first phase of the sales including 10,000 tickets 
each for the opening and closing ceremonies. Tickets can be purchased online on 
www.doha-2006.com also. Half of the purchase price for tickets will go to Reach 
Out to Asia, the Qatar-based charitable organisation that aids poor and 
disaster-stricken regions of Asia. <br>
<br>
The 15-day event begins December 1 and sees conduct of 39 sports and 423 events 
participated by nearly 10,000 athletes from 45 countries. At the time of 
booking, sports fans will receive a printed confirmation of the purchase (via 
e/mail if it’s a website transaction) which will contain all details of the 
purchase. Phase 1 commenced on 28 May and is expected to end in September. <br>
<br>
Tickets purchased in Phase 1 are souvenir tickets which will be delivered free 
of cost in November. Tickets bought from September to Games Time will be printed 
on point-of-sale thermal printers. Customers who buy tickets online from 
September will be able to collect their thermal tickets in all tickets sales 
outlets in Doha. The second phase will go until the end of the games. For the 
opening and closing ceremonies, tickets will be limited to eight per person.</font></p>
