<br>
<b>International and local pressures are leading to rise in inflation in the GCC<br>
</b><br>
Inflation 
remained at a high all over the world in 2005, particularly in the Middle East. 
Many reasons can be attributed to the soaring inflation in the Gulf region. All 
the Gulf Cooperation Council (GCC) countries are threatened by the strengthening 
of the Euro against the US Dollar. In such a situation, import bill may rise, as 
GCC countries have pegged their currencies to the US Dollar and get most of 
their imports from Europe.</font></p>
<p align="center" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<img border="1" src="gcc_dairy_7.jpg"></font></p>
<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px">
<font face="Verdana" size="1"><br>
Prices of most of the consumer items are going up making life harder in the Gulf 
region. Across the region, rents have shot through the roof, with the biggest 
jump being witnessed in the UAE and Qatar. Rents shot up by 50 per cent in 
Dubai, the commercial hub of the UAE, during 2005. The housing sector pushed 
inflation up in Qatar as well. According to a report, the housing sector drove 
inflation in the first half of 2005 by 28.45 per cent in Qatar. On the other 
hand, inflation in Oman is easing, for the reason that rents have come down by 
0.3 per cent in January-April 2005 compared to the same period in last year.<br>
<br>
According to official figures, in absolute terms, the highest rate of inflation 
in the Gulf in 2005 was recorded in Qatar, at 7.8 per cent. In the United Arab 
Emirates (UAE), consumer prices rose by 6 per cent, Kuwait recorded a modest 3.7 
per cent increase, and Bahrain less than 3 per cent. However, the lower 
inflation rate was recorded in Oman (1.1 per cent) and Saudi Arabia (0.6 per 
cent).<br>
<br>
<b>Fuel Effect</b><br>
Adding to the difficulties is the increasing fuel prices. In September last 
year, the UAE government, in response to the international market rates, 
increased oil prices by 31.5 per cent. Education and health are also proving 
quite an expensive experience for the people. Petrol, gas cylinders, healthcare, 
education, and vegetables, everything has become dearer. However, salary hikes 
are still not matching with the growing expenditures in the region. <br>
<br>
Price rises have become so forceful that the UAE government has increased the 
salaries of their employees by 25 per cent for UAE nationals and 15 per cent for 
expatriates. The Kuwaiti government, which had approved a one-off bonus of 
US$700 for government employees in December 2004, announced an annual pay rise 
of US$2,000. The previous such increase was in 1992. Bahrain’s government also 
proposed a one-off bonus of US$530, plus an annual pay rise of US$4,200 for all 
civil servants. The Saudi government announced a 15 per cent salary increase for 
government employees, marking the Kingdom’s first across-the-board shift in the 
pay scale in more than two decades. The private sector also responded quickly by 
announcing similar pay rises for its employees within days of the government’s 
announcement.<br>
<br>
<b>Low Inflation</b><br>
The pay packet fattens in the UAE, Qatar and to some extent Kuwait, are driven 
largely by inflationary pressures in the economy and increased cost of living. 
The other three <br>
GCC countries — Saudi Arabia, Bahrain and Oman — have had very low inflation 
rates. The pay rises in those markets are driven primarily by increased 
competition for a <br>
limited supply of qualified talent, says the Gulf Talent, <br>
a recruitment portal. <br>
—Sowya Sri Rallabhandi</font></p>
<hr color="#000000" width="80%" size="1">

<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b><img border="0" src="gcc_dairy_6.jpg" width="190" height="45"><br>
</b><br>
<img border="1" src="gcc_dairy_6_1.jpg" align="right" width="150" height="201">Grand 
Prix is helping transform Bahrain’s economy for the better, with the direct and 
indirect benefits that accrue out of hosting the annual event. The direct 
benefits include expenditures incurred by visitors during Formula One (F-1), 
private investors getting a chance to develop facilities and the public sector 
endeavor in expanding road network. The indirect advantages entail streamlining 
regulations and raising Bahrain’s global profile. <br>
<br>
To be sure, F-1 is a making a fair contribution to Bahrain’s economy. It is 
estimated that activities related to Grand Prix 2006 injected some US$184 
million into Bahrain’s economy. This is a sizeable amount for an economy with a 
GDP of nearly US$10 billion. Spending covers matters such as ticket sales, TV 
coverage, transportation, accommodation, food <br>
and beverage, merchandise and souvenirs, and other leisure activities.<br>
<br>
In retrospect, the government invested some US$150 million on constructing 
Bahrain International Circuit (BIC). Financing of the track was secured as part 
of US$500 million Eurobond issued in 2003, Bahrain’s first international 
financial instrument. The racing track is BIC’s primary facility. <br>
<br>
<b>Tourism</b><br>
Grand Prix has yet added another dimension to Bahrain’s tourism potentials. It 
is partially credited with encouraging developers and entrepreneurs to construct 
new hotels and other types of accommodation facilities in numerous locations 
around Bahrain, and not just Manama, the capital. For instance, a mega project, 
known as Al-Areen Desert Spa and Resort, is being built at the cost of US$750 
million near the site of BIC. The project is a joint venture between Gulf 
Finance House and the government of Bahrain. <br>
<br>
According to Middle East Economic Digest (MEED), two further elements related to 
Al-Areen were uncovered lately. Enshaa Development Real Estate Company decided 
to develop the US$100 million Al-Waha resort. Among others, the project will 
include a luxury hotel and residential, commercial and leisure facilities. Also, 
Kuwait-based Gulf Holding Company disclosed the master plan for the estimated 
US$363 million Al-Areen downtown to be built over an area of 261,000 sq. metres. 
Likewise, the project will include commercial and residential property and 
retail and entertainment facilities. <br>
<br>
Thanks - in part - to Grand Prix, several other leading hotels in the country 
have opted to expand their facilities too, giving a big boost to tourism. This 
is true of the Ritz-Carlton, Sheraton, Gulf, and Radisson SAS, to name a few 
hotels.<br>
<b><br>
Profile </b><br>
To cap it all, the event helps raising Bahrain’s profile, as the country becomes 
the global focal point of motor sporting for three days. It is estimated that 
some 450 million TV viewers worldwide watched the 2006 Gulf Air Bahrain Grand 
Prix. This extraordinary number is partly credited to an agreement between BIC 
and Al-Jazeera TV of Qatar to broadcast the event on its free-to-air channel. 
Still, Federation International Automobile (FIA) selected Bahrain as season 
opener for the 2006 Formula One World Championship. Traditionally, Australia 
serves as season opener, but not this time, thanks to the Commonwealth Games. </font></p>

<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b>Streamlining Regulations<br>
</b><br>
<b><img border="1" src="gcc_dairy_5.jpg" align="left" width="150" height="100"></b>Grand 
Prix is also helping streamlining other matters in Bahrain, notably, in doing 
away with the bureaucracy. According to officials of the General Directorate of 
Nationality, Passports and Residence (GDNPR), hundreds of F-1-related visas were 
issued for the three-day event. Visitors are offered a two-week multiple 
re-entry F-1 visas free of charge. Citizens over 30 years of age are extended 
the right of obtaining visa on arrival at the point of entry in Bahrain. <br>
<br>
Besides, hundreds of GCC citizens and qualified residents, who do not require 
visas, entered the country primarily via the King Fahd causeway linking Bahrain 
to Saudi Arabia. Yet, other nationalities need to pre-arrange the process either 
the through a dedicated website or local sponsors. No wonder that some visitors 
stay well beyond the Grand Prix, either for seeking business opportunities or 
for exploring Bahrain’s archeological jewelry. <br>
<br>
All-purpose Facility BIC is a multifaceted facility that includes numerous 
hospitality lounges, in turn used for hosting a wide range of corporate and 
social occasions. The facilities attract corporate incentive programmes, 
including entertainment, exhibitions and seminars. Located in Sakhir areas south 
of Manama, BIC’s added advantage is that of being away from the traffic snarls.<br>
<br>
Finally, two extraordinary developments unfolded in Bahrain in the days leading 
to the big event. For one, RUF of Germany laid the foundation stone for a US$50 
million facility to manufacture special cars for racers and collectors. The RUF 
Bahrain Automobile Manufacturing and Assembly plant will be built within BIC 
complex. For another, Sheikh Khalifa bin Salman highway was inaugurated, thereby 
easing travel between central Manama and Sakhir. Clearly, BIC is helping 
accelerate development throughout the country, and not just Manama, the capital.
<br>
<br>
One thing is for sure. All roads lead to Bahrain when it comes to motor sporting 
in the Middle East. <br>
— Dr Jasim Husain Ali, Director of Economic Research Unit, University of Bahrain</font></p>
