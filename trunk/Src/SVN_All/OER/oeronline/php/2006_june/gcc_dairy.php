<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<b><img border="0" src="gcc_dairy_4.jpg" width="280" height="51"></b><br>
To encourage investments in the Emirates, the Dubai government has announced the 
creation of the Dubai Corporation for Government Investment under the 
chairmanship of HH Sheikh Mohammed bin Rashid Al Maktoum, the Vice President and 
Prime Minister of the UAE and the Ruler of Dubai. According to the 19-article 
law, the new body will enjoy financial and administrative independence with full 
legal competence to assume its activities and achieve its objectives. <br>
<br>
The corporation will be responsible for laying down the investment strategy of 
government funds and enhancing the importance of the private sector’s role. It 
will also supervise the government’s financial and industrial investments and 
monitor the activities of all affiliated companies and subsidiaries. <br>
<br>
The board will lay down the general policy and administrative and financial 
regulations, which will be approved by Shaikh Mohammad. It will also issue 
bylaws, rules and decisions to regulate the corporation’s activities and prepare 
the annual budget. <br>
<br>
The Board consists of nine members. Its deputy chairman will be Shaikh Hamdan 
Bin Mohammad Bin Rashid Al Maktoum, Chairman of the Mohammad Bin Rashid 
Establishment for Young Business Leaders. The board’s term will be for three 
years and it will meet at least once every three months.</font></p>
