<font face="Verdana" style="font-size: 16pt; font-weight: 700">‘P’ for passion, 
‘p’ for pastime </font><font face="Verdana" size="1"><b><br>
Vivek C. Pande, GM, Life Style Group, Khimji Ramdas, has chartered diverse 
career paths. His pastimes are as diverse, he reveals to Mehre Alam</b><br>
<br>
</font><b><font face="Verdana" size="1">
<img border="0" src="pastime.jpg" width="150" height="156" align="right" hspace="5" vspace="2"></font></b><font face="Verdana" style="font-size: 11pt"><b>P</b></font><font face="Verdana" size="1">assionate. 
Perhaps, no ot</font><font face="Verdana" size="1">her word can define Vivek C. Pande better. His formula is simple: 
relish whatever you do. Two fingers fractured, he has driven straight to the 
coffee joint where we agreed to discuss his pastime. And there is no sign of 
discomfort on his countenance. At least it doesn’t show if there is any.<br>
<br>
On May 15 this year, Pande completed 33 years of professional life. “I do not 
feel old at all. Physically, and mentally, I still feel very young,” he asserts. 
At his age, Pande exudes some very positive energy. And his energy rubs on you. 
Very soon you realise this zealous man cannot have a mere one pastime. And the 
guess is in no way off the mark. <br>
<br>
An IIT Kanpur and IIM Ahmedabad alumnus (both ranked among world’s top 
engineering and management institutes), Pande turned to writing diary at the age 
of seven (in 1957). Pastime number one, that is. This is something that Pande’s 
close friends too are not aware of. “I have been writing as well as maintaining 
it. It’s great fun to travel down the memory lane, once in a while — the ups and 
lows...adolescent days...early 20s...the blunders etc. It is something I really 
cherish.” Does the better half try a peep into this pastime? “Well...err,” Pande 
is non-committal. But the wry smile says it all. We move on. <br>
<br>
How did he get drawn to his diary-writing passion at such an unripe age? “My 
father had brought a book written by a great Indian writer. The book consisted 
of excerpts of the daily diary this litterateur used to write. Today I met 
Mahatma Gandhi (the architect of India’s freedom)...Today I met Nehru (India’s 
first Prime Minister). That kind of stuff! This really intrigued me. That’s how 
I was drawn to diary-writing.”<br>
<br>
Pastime number two: Pande adores poetry. He may not be a wordsmith himself but 
he meticulously jots down the poetry he finds good. He is mainly into Urdu 
shairo-shairi and Hindi kavita. “Kahne ko bahut kuchch tha agar kahne me aate, 
duniya ki inayat hai ki kuchch nahi kahte... (There was a lot to say about, but 
the world stops me from saying all that...),” he recites an Urdu couplet, aptly 
describing the rendezvous at the moment. Pande remembers many an Urdu poem at 
heart. Who is his favourite Urdu poet? “Without doubt Mirza Ghalib!” Who are the 
others? “Faiz Ahmed Faiz has his own charm. But his poems were a bit high-class 
Urdu.”<br>
<br>
Cut to pastime number three. Pande is neck deep into philately. “It’s again a 
childhood pastime that I have managed to pursue. I have a collection of around 
9,000 stamps. They are from 162 countries. Every two years I do a ‘census’ of 
countrywide stamps.” When he came to Oman four summers back, how many stamps of 
Oman did he have at the time? Pande chuckles and in comes a wanton smile: 
“Before I came here I had just two stamps of Oman. Now, I have got close to 150 
Omani stamps.” No wonder that when Pande visited Istanbul recently, with wife in 
tow, one of the items on the shopping list was Turkey’s stamp. <br>
<br>
Pande is not among those who take specialization as the ultimate idea. Life, to 
him, is a much broader canvas. Doing something totally unrelated to one’s past 
interests him a lot. For one, he loves teaching. “One thing is for sure. 
Whenever I go back to India, I’ll take to teaching. It’s a favourite pastime for 
me, ” Pande reveals. There is more to it than meets the eye, perhaps! He is 
passionate about “shattering the awe created around Finance — about how ‘hi-fi’ 
this concept is!” <br>
<br>
Pande is already into this myth-shattering exercise. He organises lectures to 
explain the Finance lexicon and fundamentals to the generalists. This 
correspondent, mortally afraid of the finance juggernaut himself, laps up the 
opportunity. “Please invite me to the next lecture on Finance, Sir.” One more 
student for Pande to fulfil his pastime!</font></p>

