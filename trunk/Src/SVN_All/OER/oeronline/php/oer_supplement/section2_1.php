        <p align="justify"><font color="#000000" face="Verdana" size="2"><b><br>
        </b></font><b><font color="#000000" face="Verdana" size="4">At 
        Crossroads<br>
        </font></b><font color="#000000" face="Verdana" size="1"><b><br>
        The fixed line services are severely affected by the explosion in the 
        mobile segment in Oman but with the expected liberalization of this 
        segment the situation might improve </b><br>
        <br>
        </font><font color="#000000" face="Verdana"><b>O</b></font><font color="#000000" face="Verdana" size="1">mantel 
        has a monopoly over providing fixed line services in the Sultanate. The 
        company was established in 1980 pursuant to Law No.43 of 1980 as the 
        General Telecommunications Organization (GTO) and has been a monopoly 
        provider of fixed lines services since then. Effective August 1999, GTO 
        was transformed pursuant to Law No.46 of 1999 under the name of Oman 
        Telecommunications Company (SAOC). In 2005, Omantel was privatized with 
        private investors taking up 30 per cent of the shareholding via the 
        initial public offering route (IPO).<br>
        <br>
        <img border="1" src="crossroads.jpg" align="right" width="125" height="126">In 
        January 2006, the total number of fixed line subscribers were 255,099 
        inclusive of 230,210 post paid subscribers, 18,303 pre paid subscribers 
        and 6,586 card pay phone subscribers. The fixed line subscriber base has 
        gone up marginally only in the last one year. In January 2005, the total 
        number of fixed line subscribers were 250,720 inclusive of 231,864 post 
        paid subscribers, 12,321 pre paid subscribers and 6,535 card pay phone 
        subscribers. <br>
        <br>
        This negligible growth could be easily attributed to the explosive 
        growth in mobile subscribers. With the entry of Nawras, more competition 
        has led to decrease in mobile tariffs and increase in number of value 
        added services which has attracted the telecom users to prefer mobile 
        services compared to fixed line services.<br>
        <br>
        Omantel’s top lines and bottom lines in the fixed line segment have been 
        seriously affected by this trend. In 2003, Omantel generated RO 80.5 
        million revenue from fixed line business. This figure marginally came 
        down to RO 79.4 million in 2004. But in 2005, it felt the maximum heat 
        with the revenue coming down to just 66.2 million. The subscriber base 
        increased albeit at a slow pace of 6 per cent in the same period. The 
        dwindling revenue could be attributed to the fall in blended monthly 
        average revenue per unit. The blended monthly APRU declined from RO 28.5 
        in 2003 to RO 27.4 in 2004 and it further fell down to RO 21.5 in 2005.
        <br>
        <br>
        Moving on to the regional distribution of fixed lines in Oman (as per 
        the figures available dated August 2005), Muscat dominates the 
        statistics with 117,612 customers. It is followed by Al-Batinah region 
        (40,773), Al-Dakhilia region (23,877), Al-Sharqiya region (19,231), 
        Dhofar Governorate (17,759), Al-Dhaira (14,865), Musandam Governorate 
        (2,857) and Al-Wusta region (208). <br>
        Unfazed by the dwindling fortune of its fixed liner services, Omantel 
        has launched a wireless fixed phone lines project known as WLL Network. 
        The first stage of the project will cover 25 rural villages in the 
        Governorate of Dhofar. The project, which will be completed over two 
        stages, will ultimately cover about 200 villages. <br>
        <br>
        Dr. Mohammed bin Ali Al-Wohaibi, the Executive President of Omantel, 
        said that delivering the service of wireless fixed phones to these 
        villages is part of the company’s strategy to push forward the 
        development process in rural areas as well as expand the 
        telecommunications infrastructure in all the Sultanate’s regions and 
        villages. He added that the new system will help deliver fixed 
        telecommunications and Internet services to these villages through the 
        installation of equipment which will be granted by Omantel to its 
        customers free of charge. Besides this, Omantel launched many consumer 
        friendly offers to promote its fixed line services in 2005.<br>
        <br>
        The government has already realized that in order to expand the fixed 
        line base in the Sultanate it needs to liberalize this segment. The 
        Ministry of Transport and Communications has already taken a decision to 
        open up this segment by granting a license to the second service 
        provider. TRA is working on this front by formulating the method and 
        regulations before setting the ball rolling. It must be noted that 
        Omantel was awarded the fixed license for 25 years in 2004. The fixed 
        license imposes an annual royalty of 10 per cent of gross revenues. The 
        fixed license allows Omantel to provide local, long distance &amp; 
        international telecom service for voice, public data and all 
        telecommunication services between Oman and other countries. It is also 
        allowed to provide information services, telephone cards and leased 
        circuits etc. <br>
        <br>
        As per market expectations, the new fixed line service provider might be 
        in operation as early as the end of next year. It will be interesting to 
        see which company gets the opportunity to challenge Omantel as it is 
        going to be a tough call for the newcomer to set-up the infrastructure 
        and woo the consumers. But one thing is sure that it is definitely going 
        to rejuvenate the market.</font></p>
