        <b><br>
        National and International Coverage:<br>
        </b><br>
        Oman Mobile reaches more than 95 per cent areas in all the Wilayats of 
        Oman. Its services are spread over more than 3482 residential areas 
        through its power stations and towers located on strategic and main 
        roads in all populated areas. The company has added more than 650,000 
        lines last year – some 200,000 extra lines in the capital area, 200,000 
        lines in the Nizwa and Sur exchanges and 50,000 in Salalah. In November 
        2005, Oman Mobile signed a deal with Siemens Communications to extend 
        GSM radio capacity and coverage, enabling Oman Mobile to serve 
        significantly higher numbers of customers and extend network coverage to 
        some previously not covered areas.<br>
        <br>
        Internationally, Oman Mobile offers roaming in more than 105 countries 
        across the globe, currently having signed agreements with over 256 
        roaming partners.<br>
