        <p align="justify"><font color="#000000" face="Verdana" size="1">The 
        total group revenue as of December 2005 rose by 9.4 per cent to RO 270.6 
        million compared to the corresponding period of the previous year 
        amounting to RO 247.4 million. The company has achieved this tremendous 
        growth despite stiff competition witnessed in the mobile services sector 
        during the year.<br>
        <br>
        The operating expenses increased by 15.4 per cent to enhance the network 
        coverage and to comply with the quality of service parameters that are 
        required in a competitive scenario. <br>
        <br>
        <b>Opportunities &amp; Threats<br>
        </b><br>
        The telecom market is a growing industry and vital to the economy and 
        overall society. In the telecom industry, the level of innovation keeps 
        a high pace, in particular in wireless and broadband/Internet services. 
        There are still opportunities to increase customer base and penetration 
        levels in Oman. As the leading telecommunication service provider in 
        Oman, Omantel Group is strongly placed to compete with newcomers in 
        mobile as well as fixed line market. This is due to having a unique 
        presence, strong national brands, fully built-out and up-to-date 
        network, capable workforce, national distribution as well as systems and 
        processes in place to provide and deliver services and support 
        customers.<br>
        <br>
        The competition as such, is a threat that will lead to an erosion of 
        market share in Oman, but equally it can serve to stimulate market 
        growth, which has been witnessed in the Omani mobile market. The 
        competition in mobile sector has actually been one of the success 
        stories for Omantel Group, where Oman Mobile witnessed an exponential 
        growth in subscribers as well as revenues. In addition, liberalization 
        provides Omantel with opportunities to grow internationally, in markets 
        which also have been opened up for competition. <br>
        <br>
        Finally, competition also provides Omantel with the opportunity to 
        position itself as carrier of carriers on the basis of its unique 
        network position, thus utilizing the evolving competition to further 
        business opportunities.<br>
        <br>
        However, the expected competition in the fixed line business may turn 
        into a serious threat, if it is not properly implemented. Omantel takes 
        the view that competition is beneficial but that it needs to be 
        implemented in the right market at the right time and under the right 
        terms and conditions. While we are not certain how the liberalization 
        policy would evolve, we are confident that TRA’s sector policy would 
        provide the balanced path to further liberalization programmes based on 
        current adopted way of consulting with the market players before 
        finalizing the policy. <br>
        <b><br>
        Business &amp; Corporate Strategy </b><br>
        <br>
        The management is upbeat over the increased government thrust on 
        infrastructure and tourism based industry in Oman, which would 
        undoubtedly expand the market and create more job opportunities for the 
        local communities. Realizing this, the management has adopted the 
        multi-faceted corporate and business strategies with clear objectives, 
        which are in various phases of implementation. These are as follows:-<br>
        • Achieve leading revenue growth in a profitable fashion through 
        increasing customer base, by retaining customers and by building long 
        term relationships.<br>
        • Contain costs through efficiency enhancement programmes and by 
        leveraging group synergies.<br>
        • Prudent management of capital spending with increased emphasis on 
        value addition.<br>
        • Enhancement of internal skill and competency through well-structured 
        human resource development programmes.<br>
        • Expand in telecom related business opportunities to extend Omantel 
        footprint whilst building business development capabilities.</font></p>
