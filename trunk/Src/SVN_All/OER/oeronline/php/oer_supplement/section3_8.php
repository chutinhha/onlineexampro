        gaining up to 55 additional minutes of work time each day.<br>
        <br>
        To get maximum benefit out of mobile devices, enterprises need multiple 
        applications on a single mobile platform. The growth of wireless 
        computing belongs to the smart phone, a powerful device that extends the 
        superb voice functionality of a mobile phone into the realm of data 
        communications. These mobile devices have come a long way since IBM 
        designed the first smart phone called Simon in 1992.<br>
        <br>
        A smart phone with a keyboard, can place and receive phone calls, send 
        and receive e-mail and maintain the calendar and contacts list, 
        synchronized with the computer. One can also read Word, Excel, and 
        PowerPoint files, as well as Adobe PDF files, on certain handheld; even 
        edit them and synchronize the changes back to a PC. Applications such as 
        multi-party conference calling, integrated hands free and conference 
        call, dedicated voice key, Internet call (VoIP over WLAN), push to talk 
        over cellular, enhanced voice commands, speed and voice dialing, and car 
        kits bring a rich calling experience within reach of every mobile 
        professional.<br>
        <br>
        For the enterprise, smart phones will have an impact similar to that of 
        mobile telephony itself. Most of us already take for granted our ability 
        to place and receive voice calls from anywhere at any time. Smart 
        phones, which were initially designed primarily as mobile phones with 
        data-communications functionality along with advanced computing 
        capabilities, have the ability to handle and host a number of 
        applications letting them behave exactly like handheld computers.<br>
        Smart phones offer compelling advantages, not only to the degree that 
        they mitigate the need to carry multiple devices, but also because they 
        allow for integration of voice and data applications.<br>
        <br>
        Mobility has become more of a necessity than an option. Improvements in 
        hardware and software have made it an indispensable attribute of 
        corporate life. Wireless PDAs, laptops with high speed wireless 
        connections, and smart phones all have the ability to stretch the 
        work-desk, making the world smaller for everyone.<br>
        <br>
        Mobile devices of all shapes and sizes (smartphones, PDAs, portables, 
        USB drives) have already penetrated into the enterprises. As Bluetooth 
        enabled devices enter mainstream and faster technologies arrive, mobile 
        data access for workforce is all set to become a reality. More mobile 
        devices will be contending for accessing enterprise networks and 
        applications than ever before. <br>
        The fact remains that data services delivered to a smart phone through a 
        web browser have a long way to go before offering a consistently 
        positive user experience. The problem lies in the ability of 
        organizations to extend the reach of their internal business and 
        customer-facing applications to mobile devices.<br>
        <br>
        New devices continue to come out from the market leaders, aimed at 
        different user types and offered at different price points. Smart phones 
        are increasingly becoming a large part of the mobile phone market. 
        Advanced handset features are already common in Japan and South Korea, 
        and they are starting to appear in Europe and America. These advanced 
        handsets can be appropriately termed as pocket computers emerging from 
        the consumer-electronics.<br>
        <br>
        Phones that include operating systems and popular software applications 
        made mobile computing easier and more convenient for business 
        professionals, who no longer needed to carry around a full-featured 
        laptop to be productive. The arrival of a variety of third-generation 
        phones boasting high-speed data downloads is raising the expectation 
        today’s consumers have of wireless handsets. Many cell phones have 
        portable entertainment centers that include multimedia messaging, music 
        downloads, mobile TV, gaming, and much more.<br>
        <br>
        Gartner has predicted that the milestone of one billion mobile phones 
        sold per year will be reached in 2009. It also states that there will be 
        2.6 billion mobile phones in use by 2009-end. The smart phones are all 
        set to become the lifeline of the future and mobility will become second 
        nature to the corporate world.<br>
        <br>
        Mobile e-mail is on the verge of mass adoption, according to industry 
        analysts Datamonitor. According to the report there are roughly 650 
        million corporate e-mail inboxes worldwide today, at least 35 per cent 
        of which could be mobilized. As per the estimates, the global 
        addressable market for enterprise mobile e-mail would be around 260 
        million subscriptions, while global mobile operator revenues from mobile 
        e-mail and personal information management are expected to surpass $600 
        million by 2009, over three times those of 2005.<br>
        <br>
        The market is set to show rapid growth over the next five years, and 
        provide significant opportunities for mobile operators and mobile 
        infrastructure providers. From innocuous beginnings, SMS has proved to 
        become ‘the killer application’ for mobile operators for data services.
        <br>
        <br>
        As part of mobile strategy it is imperative for enterprises to excel in 
        the fundamentals by mobilizing the right applications. Mobile 
        applications are being rapidly accepted as an important business 
        communication medium all over the world. They provide enterprises speed, 
        freedom, flexibility, and the resources to work anywhere, at any time 
        and in any way. These technologies enable them to increase corporate 
        productivity as it facilitates reduced turn around time, increased 
        efficiency and enhanced transparency. They also help to strategize 
        future operations as they can economically interact with each other and 
        customers to get prompt feedback.<br>
        <br>
        So irrespective of which device is used new innovations that are 
        knocking the door of mobile communication will change the way 
        enterprises work.<br>
