        <p align="justify"><font color="#000000" face="Verdana" size="1">The 
        transformation effort conducted for the mobile business has been 
        effectively implemented by creation of a separate legal entity through 
        which a new business culture and capabilities have been built. It is 
        vital that the sustainability of this effort is secured. The 
        transformation of the fixed business is underway. It is important that 
        the transformation effort continues at full pace and with the required 
        capabilities and experience at hand to ensure its completeness as well 
        as effectiveness. <br>
        <br>
        The competitive framework requires different organizational capability 
        to enhance the organization’s competitive advantage. The key resource 
        that supports such change is “Right people with the right mindset.” <br>
        <b><br>
        Customer Focus<br>
        </b><br>
        Omantel is fully dedicated in its efforts to transform itself into a 
        customer focused, efficient and capable operator that meets the 
        increasing requirements of its customers. <br>
        <br>
        We have launched a series of customer-oriented initiatives in 2005. We 
        have introduced over 20 value added mobile services. The Wireless Local 
        Loop (WLL) technology has been introduced to provide telecommunication 
        services to over 200 villages in the coming years. Omantel has given the 
        contract for the implementation of the project to Chinese company Huawei. 
        The new system will help deliver fixed telecommunications and Internet 
        services to these villages through the installation of equipment which 
        will be granted by Omantel to its customers free of charge. <br>
        <br>
        The new Wi-Fi service has been introduced by us targeted at large 
        segment of laptop and mobile notebook users. It provides a lot of 
        advantages to customers, as it helps subscribers to visit the Internet 
        while moving from one place to another through hot-spots that will be 
        made available in more than 12 locations in public places. Thanks to 
        these hot-points Wi-Fi users will make very fast dial in and easily 
        finish their jobs without the mess of wires. This modern service will 
        make visiting the Internet through laptops and mobile notebooks a very 
        interesting experience in addition to the fact that it will save time 
        and effort. The new service is named “Ibhar” (the Arabic term for 
        sailing) as it allows customers to sail through the Internet and very 
        easily interact with the world. <br>
        <br>
        Another notable initiative of Omantel is the “Managed Network” services 
        to provide effective and efficient telecom solutions to government, 
        banks and other major corporate clients. We have introduced attractive 
        and value for money consumer promotions in mobile, fixed and Internet 
        services. We have extended our customer service counters and 
        distribution network with enhanced service standards. <br>
        <br>
        <b>“The Wireless Local Loop (WLL) technology has been introduced to 
        provide telecommunication services to over 200 villages in the coming 
        years. The new system will help deliver &#64257;xed telecommunications and 
        Internet services to these villages through the installation of 
        equipment which will be granted by Omantel to its customers free of 
        charge.”</b><br>
        <b><br>
        Major investments <br>
        </b><br>
        <img border="1" src="pic1.jpg" align="right" width="75" height="252">Omantel 
        injected RO 383 million in the last five-year plan as an investment in 
        the telecom sector in the Sultanate with the aim to boost the role and 
        keep this industry at par with the world. Omantel has also invested as 
        much as RO 3.5 million in the Knowledge Oasis Muscat (KOM), an IT hub 
        which helps the Sultanate migrate to the knowledge-based economy and the 
        e-governance. <br>
        <br>
        Sohar Port – Sohar Port is expected to get involved soon in a number of 
        investment projects. It will help boost the national economy due to its 
        geographical location in the Sultanate. Omantel is building up the 
        telecommunication infrastructure by using fiber optic cables in Sohar 
        Port to provide telecom services for companies and investors. 
        Infrastructure, cost projected at RO 1 million, is aimed at creating a 
        state-of-the-art work environment to enable the mega industrial 
        facilities in the port to better communicate with each other and the 
        outside world.<br>
        <br>
        Omantel will use advanced IP-based telecom technology (namely Next 
        Generation Network) in this project. This will be done through building 
        a main exchange linked with three small exchanges. It will also provide 
        services to customers outside the port. Omantel will also provide 
        wireless communications to relay voice and data in high speed through 
        new technologies including Wi-Fi and WLL. Oman Mobile, however, will 
        provide new services like VPN which provide the companies in the port 
        with a dedicated network to enable them have access to the information 
        and data. <br>
        <br>
        Omantel, in association with Sohar Industrial Port Company, will develop 
        and operate a Port Community System which will facilitate the goods and 
        shipping traffic and the information on the companies working in the 
        port and help these companies get linked to their countries of origin 
        and also get linked with the concerned authorities in the Sultanate. <br>
        <br>
        We are planning to implement our projects and investments to achieve 
        high economic benefits from Sohar Port project. This mega project will 
        provide jobs for Omanis and the company will achieve many benefits. We 
        are also mapping out a complete plan to offer the new technologies in 
        all its exchanges in the Sultanate to support other new mega projects 
        like The Wave and The Blue City.</font></p>
