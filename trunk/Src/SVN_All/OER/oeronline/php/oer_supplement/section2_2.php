        <p align="justify"><font color="#000000" face="Verdana" size="2"><b><br>
        </b></font><b><font color="#000000" face="Verdana" size="4">Omantel 
        Soaring Ahead<br>
        </font></b><font color="#000000" face="Verdana" size="1"><b><br>
        Privatization and invitation to expand its operations in Yemen, the 
        period 2005-06 has proven to be the most eventful year for Omantel in 
        its entire history</b><br>
        <br>
        </font><font color="#000000" face="Verdana" size="1">
        <img border="0" src="tv_tower.jpg" align="right" width="125" height="138"></font><font color="#000000" face="Verdana"><b>O</b></font><font color="#000000" face="Verdana" size="1">mantel 
        is the leading Omani telecom operator providing fixed, mobile and 
        Internet services to corporates, government and residential customers. 
        The Company was established in 1980 pursuant to Law No. 43 of 1980 as 
        the General Telecommunications Organization (GTO), and had been a 
        monopoly provider of telecommunications services before the entry of 
        Nawras in 2005. <br>
        <br>
        Effective 1 August 1999, GTO was transformed into a closed joint stock 
        company pursuant to Law No. 46 of 1999 under the name of Oman 
        Telecommunications Company (S.A.O.G.), wholly owned by the Government of 
        the Sultanate of Oman, as part of the pre-privatization process. In view 
        of emerging competition in the mobile market, Omantel Group established 
        Oman Mobile in 2004, as a subsidiary company. Considering the expected 
        competition in the fixed line markets, Omantel has initiated a 
        transformation programme that involves a new organization, changes to 
        systems and processes as well as internal skills. Omantel is owned 70 
        per cent by the government and 30 per cent by private investors, 
        following the Initial Public Offering (IPO) conducted in July 2005.<br>
        <br>
        During 2005, the management experienced two major events, which changed 
        the Omani telecom market. A new mobile license was issued, thus 
        introducing competition in mobile segment beginning March 2005 and the 
        Initial Public Offering of Omantel. On 28 July, 2005, Omantel shares 
        were listed on the Muscat Securities Market (MSM). <br>
        <br>
        As per January 2006, Omantel had 255,099 fixed line subscribers and 
        50,110 Internet subscribers in the Sultanate as per Telecommunications 
        Regulatory Authority (TRA) figures. Oman Mobile has a subscriber base of 
        more than 1.2 million. On the revenues front, The Omantel Group’s mobile 
        revenues have overtaken that of fixed line revenues and accounted for 60 
        per cent of total service revenues of RO 270.6 million in 2005. The 
        mobile segment has been the primary growth driver of the Omantel Group’s 
        revenues over the past three years particularly following the 
        introduction of the prepaid services in 2001. <br>
        <br>
        <b>Fixed line services<br>
        </b><br>
        Omantel’s fixed line services include national and international fixed 
        line voice, prepaid cards (Jibreen) and payphones (Al Multaqa). In the 
        case of other international and regional operators, there has been an 
        adverse impact on Omantel’s revenues from fixed line services mainly due 
        to migration of traffic from fixed line to mobile, especially from the 
        payphone and Jibreen services. <br>
        Fixed line revenues are generated from three service elements — 
        international calls, national calls and recurring monthly subscription 
        and one-time installation fees. International and national calls have 
        always been the main contributors to the fixed line revenues.<br>
        <br>
        <b>Residential Services offered:<br>
        </b><br>
        <b>1) Fixed Services<br>
        </b><br>
        <b>International Calls </b><br>
        Omantel WorldCall (International Call) service enables its customers to 
        communicate across the world at affordable rates. Off-peak rate starts 
        from 7 PM to 8 AM (varies between countries) and it is also applicable 
        on full days on Fridays and national commercial holidays.<br>
        <br>
        <b>National Calls <br>
        </b><br>
        Telephone calls are charged based on bands of radial distance with each 
        exchange as a center point except service calls which are free.<br>
        <b><br>
        ISDN Services <br>
        </b><br>
        Integrated Services Digital Network (ISDN), a set of digital 
        transmission standards that is radically changing the approach to 
        communication systems and applications around the world provides the 
        means for all forms of information — voice, images, video, documents and 
        data — to flow through a single network interface.<br>
        <br>
        <b>ISDN Types</b><br>
        <b>1) Basic Rate Access:</b> This consists of two 64kbps channels 
        simultaneously allowing for one voice and one data or two voice or two 
        data connections using one telephone number. The total bandwidth is 
        128kbps; another D-channel is used for signaling working at 16kbps.<br>
        <br>
        <b>2) Primary Rate Access:</b> It is designed for those customers who 
        are expecting to make or receive a large number of simultaneous calls. 
        The typical users of this service are large companies, hotels, 
        educational institutions and government organizations. This provides 
        thirty (30) “B” channels working at 64kbps each and a “D” channel (also 
        at 64kbps) in a 2Mb system. This is becoming a favorite way to connect a 
        PABX (Private Automatic Branch Exchange) to the public exchange <br>
        <br>
        <b>Prepaid Card “Jibreen” <br>
        </b>Jibreen is a prepaid calling card service and it’s a fixed 
        communication service, which gives the ability to communicate from any 
        fixed telephone or payphone anywhere in the Sultanate. <br>
        <br>
        <b>Sahl </b><br>
        Sahl, the new prepaid fixed telephone service from Omantel, is targeted 
        at homes and small business organizations. <br>
        <br>
        <b>2) Internet<br>
        </b><br>
        <img border="1" src="fixedline_1.jpg" align="left" width="150" height="111">Omantel’s 
        Internet and Data communications services include Internet dialup, 
        broadband services (ADSL “PowerNet”), prepaid services (“Al Ufuq”) and 
        Internet dedicated services. It also includes leased circuits (domestic 
        and international) services. Internet and data communications services 
        have contributed approximately 6 per cent to 8 per cent of total service 
        revenues of Omantel over the past three years. The revenue of Internet 
        service is boosted by the broadband service in the year 2005 and 
        increased usage of the “Al Ufuq” prepaid cards.<br>
        <br>
        As part of its continued efforts to provide all sorts of Internet 
        services to its subscribers, Omantel recently launched Wireless Internet 
        Service, better known as Wi-Fi. According to Omantel’s Executive 
        President Dr. Mohammed Bin Ali Al-Wohaibi, “The new Wi-Fi service 
        introduced by Omantel aims at attracting large segment of laptop and 
        mobile notebook users. 1.1million mobile subscribers can visit the 
        Internet through their mobile notebooks.” <br>
        <br>
        Dr. Al-Wohaibi indicated that the new service named “Ibhar” (the Arabic 
        term for sailing) shall provide lots of advantages to customers, as it 
        will help subscribers visit the Internet while moving from one place to 
        another through hotspots that will be made available in more than 12 
        locations in public places.<br>
        <br>
        <b>Expansion &amp; investments<br>
        </b><br>
        <b>
        <img border="1" src="fixedline_2.jpg" align="right" width="125" height="147"></b>With 
        competition expected to intensify further in the coming years, Omantel 
        is gearing up to meet the new challenges by focusing on enhancing the 
        internal competencies in terms of its human resources, processes and 
        systems during the last two years. <br>
        <br>
        In 2005, Omantel signed over RO 78 million worth of contracts, mostly 
        infrastructure based expansion covering both Omantel and Oman Mobile. If 
        we look at the figures in the last five-year plan (2001-2005), Omantel 
        injected investment of RO 383 million in telecom infrastructure. <br>
        Another notable development for Omantel has been the National 
        e-government and BankNet projects. The Government Convergent Network is 
        a next generation infrastructure to support e-government initiatives. 
        This is based on the latest MPLS technology. This innovative managed 
        network service will have the capability of supporting data, voice and 
        video over the same infrastructure and will ensure service delivery 
        according to defined and guaranteed Service Level Agreements (SLA). The 
        trial run is already on and is projected to be commissioned in the 
        second quarter of 2006. Similar solution will be extended to banking 
        sector to facilitate a single integrated, managed and efficient 
        telecommunication network for the banks.<br>
        <br>
        Tejari, the Middle East’s premier online business to business (B2B) 
        marketplace has entered into a partnership with Omania E-Commerce, which 
        is jointly promoted by Omantel. This new initiative will enable both 
        government departments and private organizations to source goods and 
        services through online trading hub, enhancing transparency and lowering 
        the costs associated with paper-based procurement processes.<br>
        <br>
        <img border="1" src="fixedline.jpg" align="left" width="125" height="113">Omantel 
        has also committed over RO 1 million to provide backbone telecom 
        infrastructure in Sohar Port. It is also working towards striking a 
        partnership with port authorities to establish management system in the 
        port in order to streamline the shipping process and customs through a 
        fully automated system.<br>
        <br>
        Omantel has also set-up a joint venture, Infoline LLC, which is engaged 
        in the provision and management of call center services. The non-core 
        activities of Omantel including directory enquiry services are 
        outsourced to Infoline.<br>
        <br>
        Omantel has invested US$ 39 million in the FLAG (FALCON) undersea cable 
        systems project. It is in advanced stage of building a new resilient 
        sub-sea fiber optic loop cable system connecting Oman, UAE, Qatar, 
        Bahrain, Kuwait, Saudi Arabia, Yemen, Egypt, Sudan, Iran and India. The 
        system will have two landing stations in Oman (Seeb and Khasab). This 
        project, once completed, will serve as a traffic hub for carrying 
        traffic across Middle East and the Indian subcontinent.<br>
        <br>
        It has also signed a three-year agreement with Eutelsat for the lease of 
        a full transponder of 72 mega hertz on the W6 satellite at a cost of RO 
        615,385 per annum. The agreement is in line with Omantel’s strategy to 
        strengthen network capability to meet the growing service demand. <br>
        Its strategy to make profitable investment and expand its operations in 
        the Gulf region has met with success as Omantel has been invited to 
        provide mobile phone services in the Republic of Yemen. It has been 
        asked to start negotiations with a committee established by the Yemen 
        Government on getting the license to act as a third operator of mobile 
        phone services in Yemen. Omantel had earlier submitted its bid amounting 
        to US$ 100 million to enter Yemen as the third mobile service provider. 
        This invitation is good news for the government of the Sultanate which 
        has helped the company in realizing several accomplishments in the 
        telecommunications sector in the Sultanate over the past year. In case 
        Omantel wins this tender, it will be the first big achievement of 
        Omantel since it was converted into a joint stock company.<br>
        <br>
        Saud Bin Nasser Al-Shaikili, Chairman, Omantel said: “The increased 
        investment in infrastructure such as investment in FALCON, Eutelsat for 
        the lease of satellite transponder, investment in managed network 
        services to support the e-government initiative in Oman, increased 
        investment in seamless coverage of mobile network mostly all parts of 
        the populated areas and major roads — are some of the major 
        investments/initiatives to prepare the company to position itself 
        effectively in the evolving competitive business scenario.”<br>
        <br>
        Omantel has also launched a wireless fixed phone lines project known as 
        WLL Network. The first stage of the project will cover 25 rural villages 
        in the Governorate of Dhofar. The project will ultimately cover about 
        200 villages. <br>
        <br>
        Dr. Al-Wohaibi, the Executive President of Omantel said that delivering 
        the service of wireless fixed phones to these villages is part of the 
        company’s strategy to provide telecommunications services to all regions 
        of the Sultanate and to expand area of the comprehensive development 
        process. He added that Omantel would endeavor to deliver the service to 
        all villages in the next stage. Omantel had signed an agreement on the 
        implementation of the project with the Chinese company Huawai in April 
        2005. The project will be implemented over two stages. The new system 
        will help deliver fixed telecommunications and Internet services to 
        these villages through the installation of equipment which will be 
        granted by Omantel to its customers free of charge. <br>
        The wireless fixed telephone lines project is not different from the 
        currently utilized systems. It will provide all advantages and features 
        of any fixed line connected through copper cables. The subscribers can 
        make international calls and dial in. They can also use fax, voice 
        phone, SMS messages as well as all other features of the normal fixed 
        phone lines. <br>
        <br>
        <b>Customer focus<br>
        </b><br>
        The telecommunication sector is moving from a monopoly of Omantel to a 
        vibrant and competitive market. This requires Omantel to adopt a 
        flexible and lean operating model with articulated business strategies 
        along with a competent work force, all sharing a common commercial and 
        business driven culture. Omantel is transforming itself into a customer 
        focused, efficient and capable operator to meet the requirement of its 
        key stakeholders.<br>
        <br>
        The company has initiated a number of customer service oriented 
        programmes. Omantel and Oman Mobile’s service are provided in over 42 
        service centers including 8 special exclusive service centers with 
        extended working hours. To provide the customers with enhanced choice, 
        Omantel has established the internal billing and collection system and 
        started the collection of bills. The automated bill payment machines 
        have also been introduced. <br>
        <br>
        To serve the customers in a better manner, Omantel has implemented a 
        number of programmes to further develop its internal functioning 
        systems. The company has also started Omantel Al Hail Contact Center 
        with 150 seating capacity since October 2005. The center serves over 
        half-a-million customers per month. The state of the art center is 
        equipped with well-trained staff and IVR/e-mail facilities, operation 
        management tools and outbound proactive dialing facility.<br>
        <br>
        Omantel has also begun implementing Enterprise Resource Planning (ERP) 
        based on Oracle e-suite. The first phase is expected to go live in the 
        second quarter of 2006. Once completed, the project is set to provide 
        integrated solution to finance, human resource, project planning and 
        customer service.<br>
        <br>
        Omantel Performance Management System (OPMS), being developed based on 
        International Appraising System, is expected to be implemented in 2006. 
        The OPMS objective is to evaluate employees and place them in their 
        right position based on their skills and performance. <br>
        <b><br>
        Marketing initiatives</b><br>
        <br>
        Omantel revised its tariff structure for international calls and offered 
        discounts up to 33 per cent in 2005. It offered special consumer 
        promotions with attractive offers for fixed line services, Internet with 
        PC bundle offers, broadband (ADSL) service with free modems; free 
        surfing hours etc., during Muscat/Khareef festivals, Comex’05 and 
        Ramadan. Also special Internet offers covering dial-up and broadband 
        Internet services to Ministry of Education to facilitate the enhanced 
        usage among students.<br>
        <br>
        As a responsible corporate citizen, Omantel actively supported sports, 
        educational and special needs organizations. Most notable among was 
        helping the Ministry of Education in communicating the high school 
        examination results through SMS/MMS.</font></p>
