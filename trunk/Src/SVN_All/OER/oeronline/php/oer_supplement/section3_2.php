                    <b>How has Oman Mobile performed in 2005? <br>
                    </b><br>
                    By the end of 2005 Oman Mobile had added 283,075 new 
                    subscribers to close the year with 1,089,355 subscribers. It 
                    surpassed the one million mark and retained 85 per cent of 
                    the market share. Oman Mobile has been able to double the 
                    number of subscribers through a number of transformation 
                    steps that we took to change the company from a 
                    government-owned utility public authority into a consumer 
                    and market oriented private sector Company, and this has 
                    resulted in an increased interest from the community in our 
                    services. <br>
                    <br>
                    In 2005, we came out with a large portfolio of services and 
                    achieved a full year of successful production launching over 
                    25 products and services that cater to every segment of the 
                    market in one year. We were and still are ranked the largest 
                    telecommunication distribution channel in Oman with over 
