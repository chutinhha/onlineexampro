        <p align="justify"><font color="#000000" face="Verdana" size="1"><b>How 
        has the entry of the competition impacted Oman Mobile? <br>
        <br>
        </b>The success story that Oman Mobile is now living and the success 
        that telecommunications sector is now enjoying in Oman is a measure of 
        the accomplishment of the government deregulation policy. Competition 
        has been a blessing for Oman Mobile as it has allowed us to expedite the 
        transformation process from being a government organisation into a 
        private one. The competitiveness that is shown by Nawras has kept us 
        proactive and on edge. It has inspired us to work diligently and 
        conscientiously in order to continuously innovate and to serve our 
        customers in the best possible way as well as to uphold our position as 
        the favourite mobile company in Oman.<br>
&nbsp;<br>
        <b>How is each of your services doing and contributing to the company’s 
        overall performance?<br>
        <br>
        </b>Value added services have proven to be very successful with our 
        subscribers. Oman Mobile routinely carries out customer surveys and is 
        able to develop and introduce new services based on market expectations. 
        Oman Mobile’s over 45 outstanding product and services portfolio is a 
        reflection of the company’s commitment to giving our customers a 
        rewarding experience in the world of communication. Services and product 
        list includes: Say it in Arabic/English an SMS Transliteration service, 
        SMS Parking, Tarjim, SMS-Unblock/ Block, Family &amp; Friends, Weather News, 
        Al-Jazeera News, Al-Arabiya, Sabq, and Wainak to name but a few. 
        Subscribers are becoming increasingly sophisticated and technologically 
        savvy and show a keen interest in many of the services we launch. We 
        believe that these services not only prepared us for competition but 
        they also helped prepare the society for more choices rather than on the 
        basic service with one differential, which is price. <br>
        <b><br>
        What have been your key investments in technology and customer 
        satisfaction in 2005? <br>
        <br>
        </b>Oman Mobile strives with its developing portfolio of services, to 
        provide a high quality of service and performance. With regards to 
        technology, this is achieved through its network which is of 
        international standard and ensures that the physical structure and its 
        implementation are in line with environmental and safety regulations.<br>
        <br>
        Recently, Oman Mobile completed a project (stage 7) to cover Al —Dakhiliya, 
        Al Dhahira, and Al Sharqiya regions by installing 38 base stations and 
        expanding 32 current sites in those regions. The company has also 
        provided a complete coverage on strategic roads in the regions of Bidbid, 
        Sur, Muscat and Nizwa. This included setting up 12 stations for 
        continuous service in the main areas of these roads. Another project 
        completed (stage 8) is covering the regions of Al Wusta, Musandam, Al 
        Batina and Al Buraimi by setting up 110 new sites.<br>
        <br>
        We have also made major investments in the customer service area and 
        during 2005 we launched the largest and most modern Call Centre in the 
        Sultanate. One of the technologies we acquired includes monitoring 
        /scheduling and tracking the adherence of the agent. It also calculates 
        the performance of the agent by KPI (Key Performance Indicator). <br>
        <br>
        Our new IP phones are much clearer, in terms of voice quality, for both 
        the customer &amp; the call centre agent. These phones are equipped with 
        options for our future plans which are the feasibility of communication 
        between our customers and agents through email and fax. <br>
        <br>
        Our FCMS, which stands for “Fault Complaint Management System”, is 
        another system we acquired. This system makes it much easier for our 
        customers to get to know when their “GSM” problems are solved. Once the 
        ticket number is registered for a particular customer, an SMS will be 
        sent to our customer. The customer will receive another SMS once the 
        problem is solved. <br>
        <br>
        Our IVR system provides our customers all the information they need 
        about our products and services like “Mada, Al Jazeera news, Family &amp; 
        Friends and Billing Inquiry etc.” Customers can also receive a fax with 
        the details they need “Free of charge” by using our IVR system.<br>
        <br>
        We have also hired a quality management team to ensure that the 
        performance of our employees satisfy the high standards required by our 
        valued customers. Hence, our agents are evaluated monthly on their 
        performance which will determine the kind of training the agent must 
        undergo besides the other numeral trainings the agents is obligated to 
        go through which will both benefit our customers and our agents. <br>
        <br>
        Our PDS (predictive dialing system) team makes outbound calls to 
        customers on the behalf of Oman Mobile to achieve our customer’s 
        expectations and satisfaction by asking and documenting their comments 
        about our services and products. For customers’ satisfaction, our 130 
        agents who are capable and are well qualified; work twenty four hours a 
        day, 365 days a year including holidays. Oman Mobile holds a bright 
        future for its customers where it is definitely moving from strength to 
        strength. <br>
        <br>
        <b>What are the challenges in the telecom scenario in Oman which need to 
        be addressed?<br>
        </b><br>
        With the rapid development the economy is witnessing, the challenge is 
        not only to continue to predict and analyze customer expectations but 
        also to support economic growth by building the infrastructure that will 
        facilitate that growth. <br>
        <br>
        <b>What are your plans and expectations for the year 2006?<br>
        </b><br>
        We believe that 2006 will continue to be yet another positive year for 
        Oman Mobile and we believe that our focus is to strengthen our 
        relationship with our current subscribers by meeting their expectations 
        and attracting new subscribers through promotions and marketing 
        activities. We will continue to support our corporate customers and 
        offer them Business Communication Solutions that make business sense.<br>
        <br>
        <b>Where is the growth is going to come for Oman Mobile?<br>
        </b><br>
        The growth rate will be high in value added services but the majority of 
        our revenue growth will come from voice. This will continue to grow 
        because of our call generation, call completion services which are 
        supported by the quality of our network.<br>
        <br>
        <b>Globally, telecom operators work closely with electronic media, 
        Internet </b>
        <img border="0" src="omanmobile2.jpg" align="right" width="125" height="136"><b>and 
        consumer brands to build interactivity between the consumers and 
        marketers which results in additional revenues for the telecom 
        operators.How far you have progressed on this front or taking step to 
        grow this stream of revenues? <br>
        </b><br>
        We have developed our own WAP portal which receives a great number of 
        hits. In order to connect with our subscribers at the consumer level, we 
        have also entered into partnership promotions with international 
        consumer companies such as Toyota, Pepsi, McDonalds and Alukkas Jewelry 
        to name a few, as well as local ones such as Loay Enterprises and 
        Khimjis, to extend the reach of Oman Mobile to consumer markets. This 
        has proved a valuable alternative stream of revenue for us, not only 
        financially, but it has given us a chance to offer extra value <br>
        to our customers. <br>
        <br>
        <b>We all know about the benefits of deregulation of telecommunications 
        bringing in more options for the consumers. But what are the problems 
        which crop up as a result of deregulation especially related to 
        interconnection and price regulation? </b><br>
        <br>
        In the Sultanate of Oman, the mobile telecommunications market has been 
        opened to competition in March 2005 with the entry of a new facility 
        based mobile operator. The market is still considerably regulated. Both 
        Oman Mobile and the competitor are required to obtain prior approval for 
        introducing new services or changing prices of existing services or even 
        introducing promotional price offerings. Notwithstanding the above 
        mentioned and other issues, we believe that the TRA can and should 
        examine the detailed regulations in light of the evolution of 
        competition and relax these regulations so that service providers can 
        bring new product and pricing innovations to the marketplace more 
        quickly. <br>
        <br>
        In terms of competition rules and regulations, it is expected that we 
        and the competitor hold different views on many issues but we are 
        confident that the TRA will consider all views equally and make informed 
        decisions that will benefit the customer.<br>
        <br>
        <b>What has been the attrition rate of Oman Mobile in 2005? <br>
        </b><br>
        Since the entry of a second operator, there has indeed, been some 
        limited attrition but it is important to note that since the entry of 
        the competitor in March 2005, Oman Mobile has increased its subscriber 
        base by 30 per cent at the end of the same year. We have been able to do 
        so with the introduction of a large number of value added services and 
        products that were launched. In fact, Oman Mobile had a bigger share of 
        new customers in 2005 than the competitor.</font></p>
