        <br>
        <b>What’s your plan to improve the telecommunications infrastructure in 
        emerging commercial hubs like Sohar?<br>
        </b><br>
        <img border="1" src="telecom_driving1.jpg" align="left" width="150" height="96">To 
        ensure that investors get world-class telecommunications infrastructure 
        in the emerging commercial areas, we are keeping pace by upgrading and 
        expanding our networking set-up on a regular basis. Take the example of 
        Sohar. Omantel is investing RO1 million in the development of telecom 
        infrastructure in Sohar alone. <br>
        <br>
        The company is making the use of fiber optic and setting up one main 
        exchange along with three small exchanges. It is also working towards 
        striking a partnership to establish management system in the port in 
        order to streamline the shipping process and customs through a common 
        electronic system run jointly by Omantel and Sohar Port Company. For the 
        mobile services, Oman Mobile will provide new services in the port such 
        as Blackberry and VPN among other sophisticated services.<br>
        <br>
        <b>“To ensure that investors get world-class telecommunications 
        infrastructure in the emerging commercial areas, we are keeping pace by 
        upgrading and expanding our networking set-up on a regular basis. Take 
        the example of Sohar. Omantel is investing RO1 million in the 
        development of telecom infrastructure in Sohar alone.”</b><br>
        <b><br>
        Do you think telecom development is uniformly taking place throughout 
        Oman?<br>
        </b><br>
        We are giving equal emphasis to all the regions. To give you an example, 
        Oman Mobile has recently signed three agreements to expand mobile 
        networking in Oman. The first agreement is with Ericsson Company to 
        expand mobile networking in Muscat Governate. The second agreement is 
        done with Motorola Company to expand mobile networking in Al Batinah 
        region, Musandam Governate, Al Wusta region, Al Birimi and Mohdah. And 
        the third agreement is with Siemens Company to expand mobile networking 
        in Al Dhikhlia and Sharqia region. In addition, Oman Mobile has finished 
        the first phase of mobile networking expansion in Dhofar Governate. 
        Currently, the second phase of the project is under process.<br>
        To make sure that interior parts of the Sultanate are not left 
        untouched, Omantel is connecting 450 villages with a fixed line service. 
        Already 200 villages have been covered at a cost of RO3 million.<br>
&nbsp;<br>
        The work in rest of 250 villages is in the final stage. Omantel has also 
        signed agreements for the projects worth RO45 million to develop the 
        network in Muscat, Al-Batinah, Al-Dakhilia and Al-Dhaira.<br>
        <br>
        <b>What steps are being taken to increase Internet penetration in 
        offices and homes in Oman?<br>
        </b><br>
        Omantel, the sole Internet provider in the Sultanate, is making serious 
        efforts to increase the penetration level of the Internet service. On 
        the infrastructure front, the company is introducing new technologies to 
        make Internet accessible to the common man as well the commercial 
        establishments. Besides that, the company is constantly reviewing its 
        marketing and price policy to make Internet more affordable and 
        convenient for the customers.<br>
        <br>
        On the new initiatives in Internet, Omantel has launched Wireless 
        Internet Service, better known as Wi-Fi, targeted at large segment of 
        laptop and mobile notebook users. The new service will provide number of 
        advantages to customers, as it will help subscribers visit the Internet 
        while moving from one place to another through hotspots that will be 
        made available in more than 12 locations in public places.<br>
        <br>
        Thanks to these hot points Wi-Fi users will make very fast dial in and 
        easily finish their jobs without the mess of wires. This modern service 
        will make visiting the Internet through laptops and mobile notebooks a 
        very interesting experience in addition to the fact that it will save 
        time and effort.<br>
        <br>
        <b>Do you think further liberalization of telecom is a boon or a bane 
        for the existing operators?<br>
        </b><br>
        Oman is a progressive nation and we want to be at the forefront of the 
        digital society. More competition introduced in a regulated manner in 
        phases is going to further expand our telecom sector. And this has a 
        positive effect on the national economy too as it triggers rapid growth 
        in many of the other industry sectors also. It will be wrong to say that 
        market is limited and there is no need to bring in fresh players. We 
        have seen that with the introduction of Nawras, the mobile market has 
        become much more wider. Oman Mobile, earlier enjoying monopoly, instead 
        of getting affected by the entry of the second player has benefited. It 
        has become extremely marketing savvy and has registered tremendous 
        growth last year. We have plans to open up the fixed line segment and 
        TRA is already working on this currently.</font></p>
