			<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
			<b><font face="Verdana" size="4"><br>
			A deserving recipient <br>
			</font></b><font face="Verdana" size="1"><b>Paul Krugman this year’s 
			Noble prize winner in economics has successfully bridged the divide 
			between academia and the world outside</b><br>
			<br>
			<img border="0" src="news11.jpg" width="200" height="264" align="left" hspace="8" vspace="6">Paul Krugman won the 2008 Nobel prize in economics for his work on 
			international trade patterns. The Princeton University professor and 
			New York Times columnist is the best-known American economist to win 
			the prize in decades. The Nobel committee commended Krugman’s work 
			on global trade, beginning with a 10-page paper in 1979 that knit 
			together two fields of study, helping foster a better understanding 
			of why countries produce similar products and why people move from 
			small towns to cities. In awarding Krugman the Nobel, the Swedish 
			academy said his theory helped answer pressing questions and 
			inspired an enormous field of research. <br>
			<br>
			Krugman’s work looked at how economies of scale – the idea that as 
			the volume of production increases, the cost of making each unit 
			falls – worked alongside population levels and transportation costs 
			to affect global trade. Krugman’s theory was that because consumers 
			want a diversity of products, and because economies of scale make 
			production cheaper, multiple countries can build similar products, 
			such as cars. Sweden builds its own car brands for export and to 
			sell at home, for example, while also importing cars from other 
			countries.<br>
			<br>
			<b>
			<img border="0" src="board2.jpg" align="right" hspace="8" vspace="6">Another dimension</b><br>
			Trade theory, like much of economics, till then used to be discussed 
			in the context of perfect competition: thousands of farmers and 
			thousands of customers meeting in a market and the attendant supply 
			and demand governing prices. Krugman’s theory made economists 
			realise the imperfect nature of the market and that only a small 
			number of companies in certain industries, such as autos, had 
			economies of scale.<br>
			<br>
			Krugman is best known for his unabashedly liberal column in the 
			Times, which he has written since 1999. Tore Ellingsen, a member of 
			the prize committee, acknowledged that Krugman was an “opinion 
			maker” but said he was honoured solely for his research. Krugman, 
			55, was the lone winner of the $1.4 mn award and the latest in a 
			string of Americans to be honoured. It was only the second time 
			since 2000 that a single laureate won the prize, which is typically 
			shared by two or three researchers.<br>
			<br>
			<b>Beyond academics<br>
			</b>Krugman is the rare academic economist who is also part of pop 
			culture. A YouTube video of Krugman’s joint appearance with Fox News 
			pundit Bill O’Reilly on “Meet the Press” has been viewed by more 
			than 100,000 people. Besides co-authoring textbooks, he has written 
			two best-sellers, “The Great Unraveling: Losing Our Way in the New 
			Century” and “The Conscience of a Liberal,” which has jumped into 
			the top 25 on Amazon.com. <br>
			<br>
			Krugman graduated with a bachelor’s degree from Yale in 1974 and 
			received a Ph.D. from MIT in 1977. Besides teaching at Yale and MIT, 
			he also taught at Stanford. He is a native of Bellmore, New York, 
			graduating from John F. Kennedy High School. The last time an 
			economist who was this well-known outside academia won the Nobel was 
			1976, when Milton Friedman, a University of Chicago professor who 
			starred in a PBS series called “Free to Choose,” took the prize. </font></p>
