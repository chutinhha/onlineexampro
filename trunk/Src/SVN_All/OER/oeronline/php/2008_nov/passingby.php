                    </font><b><font face="Verdana" size="4">A lifetime purchase</font></b><font face="Verdana" size="1"><br>
				<b>Peter P Schoppmann sees Oman as an important long term market 
				in the region writes Mayank Singh</b><br>
				<br>
				<img border="0" src="pass.jpg" width="350" height="284" align="left" hspace="8" vspace="6">Rolls-Royce sold 1010 cars in 2007 almost 200 cars more than a 
				year ago. It was also the first time that the company’s sales 
				crossed the magic four figure mark after the brand was taken 
				over by BMW in 2003. Emerging markets like China, India and the 
				Middle East have been important contributors to the marquees 
				success. Says Peter P Schoppmann, regional director, Middle 
				East, Rolls-Royce Motor Cars, “A number of people in emerging 
				markets tend to show their success by buying a Rolls-Royce.”<br>
				<br>
				The company has segregated the Middle East into different 
				segments. Thus countries like the UAE and Saudi Arabia are 
				volume markets. Oman and Bahrain are smaller markets in the 
				region while the greater Middle East countries like Syria, 
				Lebanon, Egypt and Yemen are potential markets. Rolls-Royce has 
				a direct presence in all GCC countries, Pakistan and South 
				Africa. <br>
				<br>
				<b>Finding its feet</b><br>
				“Oman as a country has a more introverted and conservative 
				culture, but it is an important market for us from a long term 
				perspective,” says Schoppmann. Choosing a dealer for the brand 
				is an important part of the company’s business strategy – “We 
				give our dealership only to the best in any market,” says 
				Schoppmann. Al Jenaibi International Automobiles was chosen to 
				represent Rolls-Royce in Oman in 2005-06 after extensive 
				research. <br>
				<br>
				Choosing the right dealer is important as selling a Rolls-Royce 
				is all about managing a customer relationship than just selling 
				a car. A potential customer may take weeks or even months before 
				making up his mind on acquiring a Rolls-Royce. “A Rolls-Royce 
				customer in South Africa is as demanding as a customer in Oman 
				as for most of them it is all about fulfilling the dream of a 
				lifetime.”<br>
				<br>
				<b>In a strategic shift<br>
				</b>Traditionally, Rolls-Royce was seen as a brand driven by a 
				45 years-plus age segment but the company has been working on 
				broadening its appeal to a younger age group. Its Phantom 
				Drophead Coupe has helped the brand make inroads into a younger 
				target segment. The company is confident that it will not lose 
				customers due to the ongoing global financial meltdown. For a 
				start Rolls-Royce claims that the fuel efficiency of its cars is 
				the best in its category. Second, till date the brand has not 
				had any order cancellations from any part of the world. <br>
				<br>
				Having said that, the company is keeping a keen eye on the 
				unfolding developments. Says Schoppmann, “The brand Rolls-Royce 
				cannot be completely immune to the financial crisis. We are 
				carefully monitoring the situation and will come up with 
				solutions if needed, the overall effort is to avoid a panic 
				reaction.” Overall, the company has segregated the world into 
				emerging parts (or countries) and satisfied parts of the world. 
				It accepts that the crisis in the US will impact its short term 
				forecasts, but it is unsure about the fallout on its medium to 
				long term prospects. While economic cycles may come and go a 
				legendary brand like Rolls Royce is sure to survive the vagaries 
				of finance.</font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1">
