<p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Total direct premiums of the general insurance sector in
2007 increased by 15.9 per cent over the previous year from
RO118.15mn to RO136.96mn. Reports indicated that the
national insurance companies share in the total direct premium
stood at 81.9 per cent in 2007 compared to 82.9 per cent in 2006.
The foreign insurance companies on the other hand increased
their share from 17.1 per cent in 2006 to 18.1 per cent in 2007.
To improve the services provided by insurance companies and
to promote competition for better services and benefits the
number of insurance companies was increased to 20 companies
comprising 11 national companies and nine foreign insurance
companies. The Capital Market Authority, the regulator of the
insurance sector is making efforts to disseminate insurance
awareness, to develop insurance services provided by insurance
companies and to cater for the needs of all segments of the
community such as vehicles insurance and bank assurance.</font></p>

