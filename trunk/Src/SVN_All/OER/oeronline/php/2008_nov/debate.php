                      <td width="429" height="104" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Using the tax payer’s 
						money is not justified to bail out companies. Gradually 
						there are cases coming to light wherein big business and 
						banks have been giving big bonuses and incentives to 
						their senior management and CEOs. The legal system 
						should ensure that this money is re-channelised into 
						helping institutions that need financial support. There 
						is surely a lacuna in the system as we have seen the 
						recurrence of such a crisis in 1929, the 1950s and now. 
						It shows that there is a lack of corporate governance 
						and it raises the need to bring in better regulatory 
						systems to monitor things. The fact that a meltdown in 
						one part of the world has a cascading effect worldwide 
						shows the need for an international body which 
						implements and monitors corporate governance standards.<br>
&nbsp;</font></td>
