                      <td width="429" height="96" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">The government should have 
						intervened earlier in the financial crisis. Though the 
						government has been monitoring the situation for a while 
						it is their responsibility to take preventive measures 
						so that the financial situation does not worsen. The 
						government is the custodian of a country’s economic 
						environment and so if a need arises it has the right and 
						responsibility to intervene. This does not mean that 
						disciplinary action should not be taken against 
						companies which are found to be at fault.</font></td>
