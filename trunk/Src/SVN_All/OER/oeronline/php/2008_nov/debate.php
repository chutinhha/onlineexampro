						government’s job should be to govern and not run 
						businesses. Instead government’s should focus on 
						developmental work like health, education etc and invest 
						in infrastructure, which have a long gestation period 
						such as roads, airports, rail where the private sector 
						would hesitate to invest. Government intervention should 
						be indirect to ensure that the economy is running 
						smoothly, by using monetary and fiscal tools to regulate 
						the economy so that businesses can flourish, people live 
						happily and the policies create enough jobs and wealth.<br>
&nbsp;</font></td>
  </tr>
