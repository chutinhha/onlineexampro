						<font face="Verdana" size="1">The current situation is 
						something none of us have ever witnessed, and it can 
						definitely be called “an extraordinary situation” as 
						financial institutions do not trust each other. Banks 
						are not trusting their correspondent banks and even to a 
						large extent small retail depositors have lost their 
						trust on the banks and prefer to keep cash at home 
						rather than banks. This situation is going to have a 
						major impact on all the economies, and this will affect 
						businesses world over. So under the current situation, 
						it is very important that governments must intervene 
						directly or indirectly so that confidence in the 
						financial system can be restored and day-to-day business 
						can go on. Otherwise, this is going to be a disaster for 
						the whole world.</font></td>
