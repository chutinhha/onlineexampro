		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Making a difference</font></b><br>
				<b><font face="Verdana" size="1">Soltex has been adding value 
				for its clients through its dynamic and innovative approach to 
				solutions in oil field services, writes Visvas Paul D Karra</font><br>
				</b><br>
				<font face="Verdana" size="1">
				<img border="0" src="corp.jpg" width="200" height="79" align="left" hspace="8" vspace="6">Soltex began its journey in 1997 
				in Washington. It’s focus initially was network security, 
				document imaging and staffing services. Yasseen Al Noumani, 
				founder of Soltex felt that the company had even greater 
				potential. Upon returning to his native country, Oman, Yasseen 
				met with Adil Al Jamry. Together, the idea of a “Dream Team” was 
				born. A concept that has allowed Soltex to transcend the 
				traditional idea of company structure. A team of four partners, 
				each with the same values, ideas, standards and vision. These 
				individuals are the heart of Soltex today, each sharing the 
				dream and the future possibilities. A heart that is nourished by 
				the contributions from each and every member of the Soltex 
				greater family, its staff.<br>
				<br>
				The Oil field Services Division of Soltex has been playing a 
				critical role as a service provider in the oil and gas sector in 
				Oman since early 2007 when it was awarded its first contract 
				from Halliburton for well testing and sand management system. 
				Since then Soltex has never looked back and today its order book 
				is full with contracts worth millions of dollars. Soltex offers 
				a wide range of highly complex and technical oil field services 
				including complete well test packages, supply of choke 
				manifolds, SSVs, instrumentations, valves, etc along with 
				qualified personnel to operate the equipment.<br>
				<br>
				Officials of Soltex told OER that the primary goal is to 
				continue to provide superior tools and services of the highest 
				quality by meeting or exceeding customers expectations by not 
				compromising on the quality of the tools or services supplied to 
				the oil and gas industry. The services provided by Soltex assume 
				importance in the light of the fact that PDO and the other 
				operators all have resorted to enhanced oil recovery (EOR) 
				techniques to produce oil in the Sultanate.<br>
				<br>
				<b>Professionally managed</b><br>
				Using Soltex services like water injection, cleaning up of the 
				wells, testing the wells and other methods used in EOR 
				operations, the main clients like PDO are able to increase their 
				productivity levels. As a young and dynamic company, Soltex is 
				being managed by four highly professional individuals who have 
				together between them vast experience in the oil and gas sector. 
				Abdullah Al Ismaily, Adil Jamry, Tariq Al Barwani and Yasseen Al 
				Noumani. “We are solution providers and therefore we are 
				familiar with the problems faced by our clients. We are able to 
				provide tailor-made solutions to our clients. We don’t wait for 
				our clients to come to us but we go to them. This way we are 
				able to give higher value to our clients and it has enabled us 
				to grow very fast,” says Barwani. After securing the first job 
				from Halliburton in early 2007, Soltex has been awarded with 
				well testing, SMS and water frac and disposal contracts with an 
				initial value of $20mn till 2010. <br>
				<br>
				Soltex has always strived to offer its expertise in providing 
				core solutions to its customers. Soltex has been working closely 
				with local companies outsourcing them as sub-contracts. For 
				example, a large part of the Soltex operational profile includes 
				moving equipment from one oil field to another. Instead of 
				diversifying its resources to procuring logistics support, the 
				management of Soltex decided to sub-contract this part of the 
				work to local companies. This way the company has been able to 
				focus its energy on serving its niche clients through its core 
				expertise while through its subcontracts it was able to generate 
				hundreds of jobs for the local population in the area where it 
				was working, points out Abdullah. “Honesty is our tenet, quality 
				is our life, costumer satisfaction is our promise,” says 
				Abdullah using it as his guiding motto.<br>
				<br>
				Be it in Al Noor, Qarn Alam, Fahud, Nimr, wherever the oil bases 
				are located, Soltex was able to add value to the local community 
				by providing indirect jobs to the people of that place be it 
				through transportation, manpower employment, equipment rentals 
				and vehicle leasing.<br>
				<br>
				One of the reasons Soltex could serve its clients has been by 
				being able to adapt to change. Because, the oil and gas sector 
				is constantly dependent upon innovative techniques and 
				technologies, Soltex always ensures that it stays on top of the 
				game by embracing the changes both in the market as well as 
				technologies. The oil services market has become very aggressive 
				but Soltex was able to stand on its own with a simple principle 
				of gathering vast knowledge of client requirements. Since then, 
				the company has just completed one year of its operations some 
				months ago this year but its growth cycle has been very high.<br>
				<br>
				Without mentioning any actual figures, Barwani disclosed that 
				Soltex has grown exponentially and was able to triple its asset 
				value. The company now has multiple units that provide well 
				testing, SMS, Pumping and Fracturing services, as well as 
				transmitting live data and information to the client. The other 
				intangible assets for the company have been other factors like 
				zero loss time incidents (LTI) and zero non-productive time. In 
				order to maximise the efficiency of its services and quality, 
				Soltex is also in the process of obtaining an ISO certification 
				for its integrated management system (IMS) to ensure the QHSE of 
				the company is maintained to the highest standard. Adil says 
				that as a company they believe in the Kaizen principle of 
				quality review wherein the company/ individual looks back at 
				its/his work, reviews the pros and cons and archives this 
				information so that it is available for future reference. No one 
				or nothing is perfect in this world, but a perfect system is one 
				that continuously strives to achieve the unattainable state pf 
				perfection. In this regard, Soltex has relied on its ability to 
				provide quality services to its clients by providing 
				cutting-edge solutions through competent means. <br>
				<br>
				“None of this would have been attainable without the grace of 
				God, the contribution of the team, and the support of many other 
				people and organisations, who we consider to be our partners on 
				this odyssey,” says Yasseen.With such commitment, we can only 
				say that the future is indeed looking bright for Soltex. Soltex 
				is changing obstacles into opportunities. </font>
