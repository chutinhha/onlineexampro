                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">A matter of concern</font><p><font face="Verdana" size="1"><b>The recently held environment conference in Muscat has managed to strike a consensus among the Gulf countries, which are now planning to take serious measures against the tanker owners who have persistently ignored the countries' plea to stop dumping oil waste in their territorial waters, causing considerable damage to the marine life and fishing industry, writes Lakshmi Kothaneth</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg36.jpg" align="left" hspace="5" vspace="5" width="225" height="163"></font><font face="Verdana" size="2">We consider the issue of environmental pollution as an extremely serious one. The reputation of the entire oil industry is at stake, and we also need to protect our marine life," observed HE Dr Mohammed bin Hamad Al Rumhy, Minister for Oil and Gas, Oman, while calling on the regional neighbours, during a recently held environment conference in Muscat, to help the Sultanate protect the Gulf waters from tankers discharging oil waste. The minister further stressed that pollution-free waters needed the cooperation of all the countries in the Gulf region. "We need to have a single objective and necessary measures to prevent oil pollution, since it is a common problem," he said.&nbsp;<br>
                    The Gulf, one of the most arid regions in the world, is a place where water conservation has remained a major issue for many years. Also, long spells of draughts have led the Gulf countries to increasingly depend on desalination plants to fill up their water reservoirs.T<br>
                    The conference that brought together executives from major oil companies, national environmental agencies, shipping companies, port and harbour authorities from the region managed to raise a general consensus that the regional conservation can be handled best with a joint effort between individuals, governments, international&nbsp;<br>
                    regulatory bodies, oil companies and tanker operators. Although, the Regional Organisation for the Protection of the Marine Environment (ROPME) has been taking measures to check the illegal discharge of oil into the Gulf waters, participants observed that it needs to make more efforts. "ROPME has been largely ineffective so far. They need to raise funds to set up a proper monitoring system to bring violators to justice," said a Kuwaiti port official.<br>
                    ROPME was set up in 1978 between the littoral states of Iran, Saudi Arabia, Kuwait, the UAE, Qatar, Bahrain, Iraq and Oman to co-ordinate environmental policies and fight pollution. Gulf officials and foreign environmental experts have called for an early warning system to draw maps of oil spill danger zones and set up satellite observation and regular aerial surveys. Oman considers investing in airborne radars that would monitor the Gulf waters in an eight-hour shift and take legal action against the violators. "Most of the oil discharge is done deliberately as ships clean up their tanks in order to load new consignment," said Suleiman bin Mohammed Al Busaidy, Oman's&nbsp;<br>
                    Superintendent General for Pollution Control.&nbsp;<br>
                    <br>
                    Lack of proper reception facilities has been cited as the major reason for hampering the progress in controlling the oil spill in the Gulf region. Recent reports suggest that Oman is collaborating with the United Arab Emirates (UAE) towards establishing the first reception facility at Fujairah, and is lobbying for international help to protect its waters whilst debating tough penalties on violators.<br>
                    The Gulf region contains more than half of the world's known oil reserves and produces around 16 million barrels per day (bpd) of crude, or two thirds of the world's traded oil, which passes through the Strait of Hormuz, one of the world's busiest shipping routes. An official from the Kuwait-based ROPME estimated that up to 30,000 barrels of crude is leaked into the Gulf every day from vessels loading from offshore rigs.&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg37.jpg" align="right" hspace="5" vspace="5" width="225" height="175">To minimise the effect of the deliberate discharges, Petroleum Development Oman (PDO) suggested the need for the region to set up a common system of monitoring the tankers passing the Gulf waterways. Saeed Al Maamary, Head of Terminal Operations at PDO, said the responsibilities of ensuring pollution-free waters is shared by the oil producers and the companies transporting the commodity. "Tighter monitoring controls are needed and perhaps the most important element is a dialogue between companies producing oil and the owners of the tankers," he said.<br>
                    Striking a similar note, Dr Abdulla Al Lamki, PDO's Deputy Managing Director, said the region couldn't continue reaping the economic rewards from oil revenues while ignoring the irreversible damage caused by oil spills. "It is our collective duty to stop it happening. Our countries have prospered from oil and we must not let oil dumping ruin our environment," he explained.&nbsp;<br>
                    Also, Abdullah Moyideen, Manager of Marin Department with Saudi-based Aramco, said his firm was cooperating with other countries in the region to prevent marine pollution. "We have spent around US$20 million worth of equipment to make sure the effect of the pollution is minimal and we should learn from the lessons of the Gulf War and work closely together."<br>
                    Earlier, the Gulf War caused the most horrendous marine pollution in the history of Gulf when Iraq invaded Kuwait in 1990. Iraq deliberately destroyed Kuwaiti oil installations spilling million of barrels a day in the Gulf waters. "We cannot monitor the tankers all the time. We also need the cooperation of International Maritime Organisation (IMO) and the International Convention for the Prevention of Marine Pollution  (MARPOL) to make it work," Moyideen said.<br>
                    Michael Julian, Chairman of the Marine Environment Protection Committee at the IMO said that the Gulf nations were very keen to ratify MARPOL's regulations. "But the only stumbling block that they face is the lack of adequate facilities," he said. MARPOL regulates marine pollution and requires members to strictly adhere to its regulations by conforming to certain standards like regular monitoring and reception facilities to discharge oil waste. "People say that when you fly over the Gulf coasts, you can see pollution even on the beach," Julian added. Last September, dead fishes covered the long stretches of Omani beaches, which officials blamed the severe depletion of oxygen caused by an oil slick. Similarly, hundreds of dead fish littered the coast of Fujairah last year because of oil pollution.</font></p>

