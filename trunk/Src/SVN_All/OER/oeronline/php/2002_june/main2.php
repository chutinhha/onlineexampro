                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Blessing in disguise&nbsp;</font><p><font face="Verdana" size="1"><b>Recently, Saddam Hussein turned off the taps at Iraqi oil fields, in protest of the United States' alleged support of Israel's offensive in the West Bank. He called on his oil-producing neighbours to follow suit and launch a repeat of the 1973 oil embargo, which plunged the West into a deep recession. But while Iran and Libya applauded his move, they failed to back up words with deeds. Oil continued to flow, and markets carried on pretty much as normal, writes Patrick Dane</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg34.jpg" align="left" hspace="5" vspace="5" width="225" height="200">In the surface, Saddam's sacrifice was completely in vain. After a month of withholding some two million barrels per day (bpd) - roughly three per cent of total global production - Iraq began pumping oil again in early May. "The Iraqi oil embargo has fizzled out for lack of Arab support," said Standard Bank analyst William Buchanan in a recent report.<br>
                    However, it soon became apparent that Saddam's gesture had had a significant impact - albeit not as extreme as he would have liked. Iraq's decision to halt exports sent US inventory levels plummeting in mid-May. The US is one of Iraq's biggest customers, and the mini-embargo starved the US of crude imports. The result: a sharp increase in prices.<br>
                    A report by the American Petroleum Institute (API) showed a surprisingly large 7.4 million barrel fall in U.S. crude stocks in mid-May, putting stocks below year-ago levels for the first time in 2002. The inventory drop was much greater than the market expectations of a 2.5 million barrel draw.<br>
                    Benchmark NYMEX June light crude futures shot to a new eight month high of US$29.54 as soon as the report was released. This followed a gain of 98 cents the previous day in New York as traders anticipated a supportive API report. Oil prices had not been above US$29 since September 14, when it resumed trading after the September 11 attacks on America.<br>
                    Indeed, the bullish API data backed a warning from the West's energy watchdog, the International Energy Agency (IEA), that market fundamentals look tight for the next six months due to Oil Producing and Exporting Countries' (OPEC) cutbacks. OPEC production, at its lowest level since 1993, could push prices up later this year when demand is expected to recover.<br>
                    "Oil market fundamentals, including the loss of 40-50 million barrels of Iraqi crude, point to a significant future tightening of the markets," said the IEA in its May Monthly Oil Market Report. "This has, to some extent, already been reflected in crude prices, together with the geopolitical risk premium that waxes and wanes along with the Israeli-Palestinian conflict, offsetting weak product demands and comfortable inventories." The current war premium, which also reflects fears of a US invasion of Iraq, is estimated at between US$3 and US$6 per barrel.</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg35.jpg" align="right" hspace="5" vspace="5" width="195" height="300">In effect, Iraq's decision to suspend production was a shot in the arm for international oil prices which were reeling under a global slowdown and weak demand. This point was stressed by the Energy Information Administration (EIA), part of the US Government's Department of Energy. "April marked the second consecutive month that the OPEC basket price finished above US$22 per barrel, the lower end of OPEC's target range for the OPEC basket price." Although use of this target range had been suspended after the price drop following September 11, OPEC is beginning to focus again on an appropriate range for its price.<br>
                    What does the future hold? In the short term at least, high prices. US oil inventories are one of the most influential statistics in the trade, and even small fluctuations lead to big swings in the oil price. "It is expected that OPEC's production cuts and other market fundamentals will continue to push world oil prices up," says the EIA. "As inventory draws in the Organisation for Economic Cooperation and Development (OECD) countries validate that the January 2002 quota cuts are reducing supplies, West Texas Intermediate prices could rise to almost US$30 per barrel in 2003 unless the OPEC countries increase their production significantly in the latter half of this year."On balance, prices are unlikely to average such high levels. OPEC is increasingly aware that oil at US$30 has powerful long-term effects on the industry that damage the cartel's interests. First, US$30 per barrel chokes off economic growth, which is the engine that drives oil demand. Second, it makes expensive new oil fields, such as deep water Angola, economically viable - and that poses a long-term threat to OPEC's marketshare.<br>
                    For these reasons, OPEC's efforts to improve compliance with its quotas levelled-off in March and April, and more OPEC production is expected in the coming months. However, OPEC quotas have been set at very low levels, the result of repeated quota cuts totalling over five million bpd over the past year. As a result, world oil markets could tighten and oil prices may rise despite little or no demand growth. OPEC ministers have recently begun to stress this point. Saudi Arabian Oil Minister Ali Naimi suggested that while the level of oil stocks is "fairly good" now, "that does not mean a month or two months from now it's going to be good, and we will probably have to take action to increase the supply side."&nbsp;</font></p>

