<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><b><font face="Verdana" size="4">‘Being Well: Occupationally and 
Educationally’</font></b><font face="Verdana" size="1"><br>
<br>
Occupational Health is that branch of medicine which deals with the interaction 
between health and work. And, when it comes to occupational health, Petroleum 
Development Oman believes that, for the nation as a whole, the bottom line can 
be simply stated: healthier is wealthier. For that reason, PDO is holding the 
first-ever Regional Occupational Health Conference in Muscat from December 11-13 
— with some 300 international participants, delegates and exhibitors. Abhijit 
Sinha interviews Dr. Nasser S.M. Al-Maskery, chief medical officer of PDO, and 
progenitor of this unprecedented event. <br>
<br>
<i>What’s your role as conference chairman?</i><br>
To promote Occupational Health in the region in general, and the country in 
particular. We have invited professionals (see Box A) from all over the world 
who would be coming here to tell us about the best practices from their own 
countries. We also made it a point to include our own local and regional 
participants (see Box B) who would talk about Occupational Health from their 
perspective — how it should be practiced locally as well as regionally.<br>
<br>
The theme of the December conference is ‘Together — Promoting Regional 
Occupational Health’. Please elaborate.<br>
Occupational health is not a subject exclusively for medics and paramedics. It 
is a multi-disciplinary subject, and not necessarily limited to the white-coat 
doctors and nurses. We have therefore to involve other professionals too. For 
example, in the PDO, we have Environment Advisors in the interiors who are not 
from the medical profession. They are people who have certain health, safety and 
environmental responsibilities, and Occupational Health is one of the important 
standards they must enforce. And it’s not just the Oil and Gas industry. 
‘Together’ means together within the country, and together within the region. A 
comprehensive togetherness — that’s our aim. <br>
<br>
<i>What’s the basic thrust of the conference?</i><br>
The basic objective is merely to make people aware of the importance of 
occupational health. Currently, there isn’t sufficient awareness in Oman. It’s 
understandable why employers here have not moved forward in this area: by and 
large they are completely unaware of fitness standards for employees. We will 
also go into the physical hazards and ris–k as well as the non-physical aspects 
— subjects related to the fitness standards of employees in different jobs. It 
has to be understood that fitness standards are risk-based, which means they are 
tied up closely to Health Risk Assessments (HRA) of particular jobs, and matched 
to physical and mental fitness of an individual for a particular job. It’s not 
just a question of declaring an individual fit or unfit. It has to be specified 
unfit for what — because an individual may be unfit to do a certain aspect of a 
job, but not necessarily unfit to do everything. <br>
<br>
<i>Do these fitness standards vary from region to region, or are universal?</i><br>
In general terms, they are universal. There’s an agreed universal criteria of 
health. Also, there’s an agreed criteria on illness — when we talk about 
Occupational Illness or, for that matter, Occupational Disability. So, it’s 
unlikely that someone can be well in Country A but not well in Country B if both 
countries adhere to Occupational Health legislations and criteria. Of course, 
these criteria, like many other health criteria, originate from the World Health 
Organisation (WHO) and International Labour Organisation (ILO). Basically, they 
are generic in nature and are applied by almost all countries.<br>
<br>
<i>Will this conference throw up new parameters?</i><br>
No, this conference will not create anything new. But it will make sure that 
whatever is already known, published, practised, and adhered to in the developed 
countries is also applicable to our countries. In a sense, this conference will 
be more of an eye-opener — bringing together people from different disciplines 
to create a package of Occupational Health legislations, culture and practice.<br>
<br>
<i>What then will be the concrete results you expect from the conference?</i><br>
First, to put in place a comprehensive Occupational Health plan. A strategy that 
will specify Occupational Responsibilities and Accountabilities. This will have 
to be across the board, from the leader to the shop floor.<br>
<br>
Second, to introduce risk-based fitness standards for various jobs. This will be 
as per the health-risk assessments of those job categories, and take into 
account not only what you do physically but also what’s involved in that job — 
like how you travel to work, what’s the weather like, how much hot and/or noisy 
is the workplace, whom do you interact with…and so on. <br>
<br>
Third, to establish a reliable database which will identify the nature of 
problems that exist in various job categories, and health problems in different 
geographical locations or a department or a company. Again, this will be based 
on risks in the environment, together. The database will also serve for research 
purposes and fact finding exercises which should be happening on a regular 
basis.<br>
<br>
Fourth, to establish Health Risk Assessment (HRA) and Health Impact Assessment (HIA) 
guidelines. HRA is hazard inventories at the workplace, translated to risk at a 
particular time, depending on exposure — and identified as per job categories.<br>
<br>
A fifth aim is for us to work closely with the media on Occupational Health 
promotional programmes — targeted at the population at large.<br>
<br>
Finally, I must mention that a group of experts — including our colleagues from 
the Ministry of Health (MoH), Ministry of manpower (MoM), Sultan Qaboos 
University (SQU), Public Authority for Social Insurance (PASI) and voluntary 
organisations — will sit together and formulate recommendations based on the 
discussions. These will then be compiled and framed, to be presented to the 
authorities for their consideration. The recommendations will be comprehensive — 
on how to organise, practise, promote, train and research occupational health in 
this country.<br>
<br>
<i>What’s the response so far?</i><br>
It varies from information finding to excitement. From the employers, it’s 
mostly curiosity — about understanding the relationships, in both a positive and 
negative sense, between an individual and his/her work. Our colleagues from the 
universities, from Oman and elsewhere, as well as the WHO are excited. For our 
medical fraternity, it’s what we are all about!<br>
<br>
<i>So, Occupational Health is not just laws or rules but more of voluntary 
preservation of values, to be treated seriously by all?</i><br>
Absolutely. WHO defines health as ‘being well positively — physically, mentally 
and socially’. In my perspective, health is also ‘being well’ Occupationally and 
Educationally.<br>
