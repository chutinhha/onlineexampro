            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" style="font-size: 14pt"><b>Basics of boom</b></font><font face="Verdana" size="1"><b><br>
            Backed by strong economic indicators, the real estate rush hour in 
            the Sultanate will not end soon</b></font></p>
            <p align="center" style="margin-left: 10px; margin-right: 10px">
            <img border="1" src="cover7.jpg" width="300" height="192"></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">he Economist Intelligence Unit (EIU) expects Oman’s economic growth 
            to accelerate to 6.5 per cent in 2006, as high oil prices buoy 
            domestic demand and push up the Sultanate’s oil export earnings by a 
            substantial 27.5 per cent. This is the predicted scenario despite 
            the ongoing fall in oil production. Combined with a surge in LNG 
            output, this should be sufficient to raise the total export revenue 
            by 30 per cent — to US$24 billion. <br>
            <br>
            It’s a perfect recipe for real estate boom — and that’s precisely 
            what’s happening in the Sultanate. The current oil cycle, 
            accompanied by a significant rise in money and credit growth, has 
            contributed to a surge in property and equity prices. <br>
            <b> <br>
            Look at the indicators:</b><br>
            </font><font face="Verdana" size="2">&#9679;</font><font face="Verdana" size="1">
            There’s been a major price appreciation in the residential segment, 
            and rents have leapfrogged by almost 25 per cent, on a year-on-year 
            basis in 2005.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">Land/plot prices have shot up — from RO100 per sq m to over RO220 
            per sq m in less than a year — in the seaside areas adjacent to the 
            mega projects undertaken by the government in tourist designated 
            zones such as Al Azaiba, which is close to The Wave project.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679;</font><font face="Verdana" size="1">
            Sohar industrial area is seeing increased activity, thanks to the 
            plethora of projects coming up there.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">More and more hypermarkets and mega shopping malls are coming up.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">Expatriates are lining up to buy property in The Wave, buoyed by the 
            recent announcement of foreign freehold ownership law.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">The government is distributing more plots to the nationals, 
            registering manifold increase over the past few years. The total 
            number of plots distributed, have increased from 11,925 (in 2003) to 
            35,359 (in 2004), with the residential sector capturing the biggest 
            share — 85.7 per cent of the total number of plots distributed in 
            2004.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">Buying bigger or fancy homes is becoming more affordable.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">Bank home loans have become more attractive.<br>
            <br>
            </font><font face="Verdana" size="2">&#9679; </font>
            <font face="Verdana" size="1">GCC investors for some time now have been engaged in speculative 
            buying in Oman, pushing up the prices.<br>
            <br>
            But the real estate rush hour is a very recent phenomenon, unlike 
            the scenario in some of the GCC neighbours. “This dramatic shift in 
            the real estate fundamentals is less than a year old, but expected 
            to gain momentum,” says Jose Chacko, Corporate General Manager, 
            Habitat International LLC, a property management solutions company 
            (an enterprise of Groupe VTS).</font></p>
