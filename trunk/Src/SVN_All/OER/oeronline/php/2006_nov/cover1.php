            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Macroeconomics</b><br>
            “It’s all about macroeconomics,” explains Dr. Badr El Din A. 
            Ibrahim, Economic Expert at the Ministry of Finance. “Beside 
            speculative buying, the boom in real estate in Oman is a reflection 
            of the supply and demand factors driven by the public sector fiscal 
            position. The oil-based demand has driven ample liquidity in all 
            markets, including the real estate market.” <br>
            <br>
            Dr. Badr adds: “Unstable shares prices, as well as limited 
            investment opportunities, have made the real estate sector a good 
            alternative for many individual investors, fuelling the real estate 
            boom further. In fact, real estate investment in recent years has 
            not only been a profitable and safe investment, but also provides 
            solid security for borrowing.” <br>
            <br>
            <b>Speculative Buying Spree</b><br>
            “The recent spurt in property prices in Oman has come about mainly 
            because of speculative buying by nationals and GCC nationals. The 
            sudden increase in demand of properties has created a gap between 
            demand and supply. This situation will continue for a short to 
            medium term, for about five-six years, till this gap is bridged by 
            coordinated efforts between the Government and private sector,” says 
            Chacko.<br>
            <br>
            Wael Ahmed Al Lawati, the Acting CEO of The Wave, is convinced that 
            there’s a growing speculative market in Oman vis-a-vis land/plots 
            where prices are beginning to appreciate a 100 per cent every six 
            months. “It’s an incredible increase,” he says. “I think there is a 
            bit of euphoria in the market that I hope will be sustained by 
            actual projects, and will not be based on unreasonable expectations 
            of price appreciation. I think the government is aware of that.”<br>
            <br>
            A crucial element of all this has been the speculative buying spree 
            by the GCC nationals, mainly Kuwaitis, in the Sultanate. The fact 
            remains that despite the boom and the soaring prices, land in Oman 
            comes cheaper compared to elsewhere in the GCC. So, the price-push 
            will continue to be fuelled by the buying spree of GCC nationals, 
            especially in the tourist-designated zones.<br>
            <br>
            Explains Al Lawati: “Decisions taken by The Wave will give impetus 
            to the market. From the time our project started till the first 
            releases completed, we have been hearing from market sources that 
            land prices have been appreciating by nearly 100 per cent.”<br>
            <br>
            The upthrust is also expected to peak the service sectors once the 
            project reaches its final phase. No wonder The Wave has impacted 
            prices not only in Azaiba but also in Ma’abela (another area in the 
            vicinity), where prices of plots have gone up by 20 per cent in 2005 
            — and another 20 per cent hike is expected by the end of this year.<br>
            <br>
            The real estate might just be entering a more mature phase, where 
            people realise that the risks they were taking were very well 
            calculated and likely to pay off. Says Al Lawati: “We should begin 
            to see a movement from just pure land and plot trading to a stage 
            where people go for construction, so that they can service the needs 
            of staff (of The Wave) initially, and thereafter, the neighbouring 
            residents.”<br>
            <b><br>
            Key Drivers </b> <br>
            Let’s focus on the internal dynamics of the real estate sector in 
            Oman. Three dimensions — demography, liquidity and government 
            regulations — stand out as the key drivers.<br>
            <br>
            Demography: More than 50 per cent the Omani population is below 25 
            years of age. This, by itself, fuels growth in the real estate 
            sector. The other element is landscape, which allows horizontal 
            expansion in cities like Muscat, Salalah, Sohar, and Sur. So, there 
            aren’t any geographical factors limiting growth. <br>
            <br>
            Liquidity: Surging oil prices continue to bring in additional 
            revenue to the government — in turn boosting the entire 
            infrastructure in terms of new projects and yielding more liquidity 
            in the market. Deregulation of mortgage finance is the fresh factor 
            that has further boosted the real estate boom. There is a second 
            element of liquidity. Historically, commercial banks in Oman focused 
            mainly on commercial lending and personal consumer lending. They 
            used to deal in real estate lending, but it wasn’t really mortgage.<br>
            <br>
            The Central Bank of Oman (CBO) recently allowed commercial banks to 
            enter into mortgage financing, setting the current limit to 5 per 
            cent of the bank’s portfolio. The decision was widely seen as part 
            of the government policy of deregulation and enhancing 
            competitiveness. The big banks’ total portfolios are currently 
            bursting at the seam. That’s the immediate liquidity available in 
            the local market — cause enough to pep up the real estate market, at 
            least in the medium term. <br>
            Interest rates are being cited as the third factor to the liquidity 
            spillover. With the Omani Riyal pegged to the stabilising US dollar, 
            the impact is bound to be positive for the real estate lending scene 
            here.<br>
            <br>
            Government policy/regulations: The CBO has already moved in to 
            deregulate mortgage finance. The government policies have been timed 
            to encourage foreign investments — giving the go-ahead to mega 
            projects such as The Wave, The Blue City and Yitti, besides bringing 
            in the freehold ownership. All this has impacted the real estate 
            scene in a big way. <br>
            <b> <br>
            <img border="0" src="cover8.jpg" width="150" height="212" align="left" hspace="5" vspace="2">Mega Projects</b><br>
            Oman’s mega projects like The Wave, Yitti, Port of Sohar, Salalah 
            Free Zone and The Blue City are likely to give a huge fillip to the 
            property market boom. The Wave alone will build 3,500 residential 
            units. Yitti is to build another 2,500 houses, and The Blue City 
            plans to accommodate 200,000 people. <br>
            <br>
            Sohar is likely to see housing for 30,000 people once the Port of 
            Sohar and the industrial area become operational, creating demand 
            for lower end houses. Already rentals are touching a new high in the 
            Sohar city. The increase in demand in Sohar is likely to take the 
            shape of existing tenants migrating to new, higher-quality 
            apartments. <br>
            A number of mega residential projects in Sohar have already been 
            announced. Many more are in the offing. And it is all likely to come 
            up with some attractive mortgage solutions too, something on the 
            lines of the The Wave-Alliance Housing Bank tie-up. It’s big time, 
            and its fuelled by the really big projects. <br>
            Regional Comparison <br>
            One of the similarities between Oman and the UAE’s real estate boom 
            is external investments and mega projects. The boom in both markets 
            is not locally driven.<br>
            <br>
            The other similarity is in terms of the component. The projects in 
            Oman are likely to be very similar to that in the UAE — five star 
            hotels, the types of houses, marinas, golf courses and other 
            integrated tourist projects. <br>
            <br>
            But, cautions Al Lawati, “Dubai is looking at 10 million visitors by 
            2007, and 15 million by 2010. So they <br>
            are really looking at expanding the market at a very high growth 
            rate. I don’t think that’s the policy we have here.”<br>
            <br>
            The Wave Deputy CEO adds, “I think Dubai is several years ahead of 
            us. It’s probably going through a slight slowdown now, but not a 
            slump. The Dubai real estate market is more mature. Buyers are 
            asking more questions. Becoming more aware of what their rights and 
            obligations. Raising questions about the quality of the 
            construction, and whether the developers are serious or not.”<br>
            <br>
            Further delineating the differences, he says, “We cannot have too 
            many projects that are reasonably priced. I don’t think the Omani 
            market can afford to start working on that scale. I don’t think the 
            market wants to proceed with cheaper and cheaper homes. We aim at a 
            certain segment — the high-spending people who’ll obviously 
            appreciate and protect Oman’s culture, and not those who try to 
            transform our culture. I think these are some of the considerations, 
            because of which we take a slightly different view compared to some 
            of our neighbours.”<br>
            <b><br>
            Challenges</b><br>
            For the real estate market riding an unprecedented boom, there will 
            always be some challenges to cope with. Dr. Badr, the economic 
            expert in the Ministry of Finance, says: “The real estate boom in 
            Oman and neighbouring countries is a reflection of the escalation in 
            both demand and supply in recent years. But speculative buying has 
            made prices climb to exorbitant levels, and do not reflect the real 
            purchasing power of a majority of the population — the middle and 
            lower income groups. The IMF has described the rise in real estate 
            prices in the region to be near record level. This may lead to 
            excess supply, and hence an eventual collapse of the real estate 
            market. The impact of such a collapse might transcend the real 
            estate market to include the banking system, like failure of loan 
            repayments. In such a scenario, banks might find themselves unable 
            to back these real estate projects.” <br>
            <br>
            So what’s the way out to avert such a tragedy? Dr. Badr says, “The 
            banks should shift their real estate lending strategy from lending 
            to the luxurious and market-oriented construction to the 
            market-need, lower-cost real estate projects. This could be a 
            suitable long-term strategy to avoid the pitfalls of a market 
            collapse, especially with regard to the needs of the Omani youth 
            population.”<br>
            <br>
            Dr. Badr also advocates the carefully channelling of the excess 
            liquidity created by the oil price surge into new, diversified 
            productive avenues. “This distribution of liquidity among productive 
            sectors is likely to provide more growth and employment 
            opportunities — and will protect the economy from any sectoral 
            market collapses, including real estate market collapse,” he opines.</font></p>
