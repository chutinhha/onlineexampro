      <p align="justify"><font face="Verdana" size="1">
      <b>LESSONS TO PROCUREMENT DIVISIONS:</b><br>
      Do not leave it to chance; understand whose terms govern the relationship 
      with the supplier at the outset. If it is the suppliers’, understand them!<br>
      Draft your own standard terms with buy in from finance and other 
      departments.<br>
      Have safeguards in place against accidental acceptance of supplier’s terms 
      — undertake an audit to avoid falling into this trap.<br>
      Make important issues explicit, for example, price, time of delivery and 
      consequences of delays.<br>
      <br>
      <b>POST-TENDER STAGE FOR HIGH VALUE CONTRACTS:</b><br>
      Mark all documents “Draft — Subject to Contract.”<br>
      Avoid entering into acceptance letters. Enter into one final contract.<br>
      Convert suppliers’ sales language into clear obligations before 
      incorporating them as part of the final contract.<br>
      Do not be afraid to discuss allocation of risk at contract negotiation 
      stage for late delivery.<br>
      Do not include general vague terms that are difficult to police.</font></td>
