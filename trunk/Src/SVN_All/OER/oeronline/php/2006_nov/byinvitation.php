<br>
<br>
</font><font face="Verdana"><b><font style="font-size: 14pt">Technology 
Contracts Decrypted</font></b></font><font face="Verdana" size="1"><br>
<b>Most people will get confused with the concept of Information Technology (IT) 
contracts. Saleem Ashrafi Adam takes us through what IT law is and some 
interesting issues surrounding IT-related contracts. This article is part one of 
a two part series.</b><br>
<br>
</font><b><font face="Verdana" size="2">
<img border="0" src="pic42.jpg" align="right" hspace="6" width="100" height="134">A</font></b><font face="Verdana" size="1">t its simplest, technology is either 
bought and sold or used for doing things previously done in different ways. 
Thus, most existing commercial laws still apply to IT and IT-related 
transactions and it is simply the context that has changed.<br>
<br>
Of course, there are some areas of law that are particularly relevant to 
technology. These often include: (1) Regulatory issues specific to the industry 
(for example, telecommunications or banking); (2) Specific copyright laws 
applying to the ownership of software and databases; and (3) Data protection 
legislation and privacy regulations may also apply, but these are currently not 
relevant in most parts of the Middle East. Before we consider the specific 
matter, let us look at the basic concept of what a contract is.<br>
<br>
<i>What Is A Contract?</i><br>
We are familiar with the concept of an “agreement” or a “deal” — these are terms 
we use in everyday conversation. The idea is that two people have come to a 
settled, shared view about something — say whether to start a company or which 
restaurant to go to that evening. <br>
<br>
In legal parlance, however, a “contract” has added significance. In essence, a 
contract is where two or more parties enter into an agreement that is 
enforceable before the courts. A failure to comply with such an agreement, 
therefore, has legal consequences. <br>
<br>
<i>When Does A Contract Come Into Existence?</i><br>
Lawyers look for several elements to be present for a contract to come into 
being. However, for the purposes of this article, we will consider “offer” and 
“acceptance”. The critical question is at what point does one party make an 
unequivocal offer to do or not to do something, which is unequivocally accepted 
by the other?<br>
<br>
The real issue is one of timing. In a business context, the parties need to know 
when the contract comes into being, and on what terms it exists, i.e. in 
agreement. Lawyers will be careful to mark agreements and correspondence 
exchanged as “draft — subject to contract”.<br>
<br>
Matters are more likely to go wrong in less sophisticated negotiations (where 
lawyers are less likely to be involved). A common example is where, on a 
straightforward procurement, parties deal with each other by swapping standard 
forms — the buyer uses standard procurement terms while the seller uses its 
standard sales terms. In such cases we need to identify when the contract was 
formed so we can work out whose terms apply. As mentioned above, this is a 
question of fact, which is why it is vital that procurement departments have 
procedures in place to safeguard against the accidental acceptance of a 
supplier’s terms.<br>
<br>
<i>What Are The Terms Of The IT Contract?</i><br>
The parties naturally need to know what is it that they have agreed to do. In 
many situations, such as the simple sale of a CD, the terms are unspoken and 
obvious; the seller transfers ownership of the CD to the buyer in return for 
money paid by the buyer to the seller.<br>
<br>
However, even in these relatively simple circumstances, the reality is more 
complex. The ownership of the physical CD is transferred, but the contents of 
the CD remain the property of someone else — the person whom owns the copyright. 
The buyer has purchased the CD, but only has the right to listen to its contents 
and not, except for limited purposes, to copy the CD. In all IT transactions, 
the parties will actually want to spell out what the terms of the IT contact 
are.<br>
<br>
IT contracts therefore need to clearly deal with these otherwise unforeseen 
complexities. The express terms of the contract will typically deal with all of 
the major issues.<br>
<br>
For example, we often have clients seeking advice on whether they should pay tax 
or carriage costs on hardware orders for IT contracts. Since the IT contract has 
not been explicit on this, the suppliers tend to charge customers and this comes 
as a surprise to customers. It is important to identify what the price includes. 
If you are purchasing a piece of hardware with embedded software, then you 
should be explicit on what the price includes i.e. “The price includes 
manufacture, supply, delivery and off-loading, packaging, labelling, carriage, 
insurance, royalties, licence fees and all other charges, taxes, duties and 
impositions and is not subject to alteration for any reason whatsoever”.<br>
<br>
Often IT contracts are littered with vague and general terms. It is worth noting 
that the people who initially negotiated the contract may not be around to 
decrypt any vague clauses in a contract 12 months after the contract has been 
executed. Therefore, everything should be clearly expressed in the contract.<br>
<br>
For example, if parties to a contract leave the price to be agreed as a 
“reasonable fee”, then there is significant room for a dispute to arise. This is 
because the parties are likely to hold different views about what is 
‘reasonable’, and both views may have merit depending on whose side you take.<br>
<br>
From the purist lawyer’s point of view, of course, vague clauses such as the 
above example have no place in a contract because they cannot be relied on. The 
reality is that they are often used as a cosmetic exercise to indicate the 
relationship between the parties. This is fine: as long as both parties 
understand that that is all they are.<br>
<br>
<i>How Should Risk Be Allocated In High Value IT Contracts?</i><br>
There are other factors which make doing deals in the technology sector more 
complicated (and therefore interesting!) than doing deals in others sectors. 
These include, for example, the difficulties of allocating risk and reward when 
technology is used as a business enabler, and specific issues that may arise in 
ensuring that purchased technology performs and is maintained to everyone’s 
satisfaction.<br>
<br>
<b>Scenario</b><br>
A new insurance company is intending to launch on March 1 in order to establish 
itself in a new market. It launches an advertising campaign as a run up to the 
launch date. Billing Supplier knows this and project plan states that the 
billing system will be live by March 1. Billing Supplier has a problem and the 
launch date has to be put back for until April 1.<br>
<br>
<i>Should Billing Supplier be liable for the wasted advertising costs? And 
should it be liable for any potential lost business?</i><br>
Suppliers will regard the losses cited in the above scenario as “consequential 
losses” and they will not enter into debates as to whether or not they should 
even be included in the contract. Lawyers will often argue as to what 
consequential loss is and what direct and indirect losses are. All jurisdictions 
define consequential loss differently. In fact, there is nothing in the law to 
prevent customers from making sure that certain losses are clearly specified in 
a contract so that any risk is incurred by the supplier.</font></p>

<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="pic61.jpg" width="450" height="223"></p>

<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">For example, it is common to see contracts drafted where it is made clear that 
wasted advertising costs and potential business loss can be claimed (scenario 
above). It is possible to do this where the financial loss is pre-estimated at 
the outset and included as part of the contract — through service credits or 
liquidated damages, which shall be more particularly discussed in Part 2 of this 
article. This is just one example of how IT contracts differ from normal 
contracts so evidencing the customer’s particular intention that the IT system 
should make a clear financial difference to its business. It is absolutely 
imperative that the contract is adequately drafted and addresses all of the 
client’s issues.<br>
<br>
<b>Referring To Tender Documents</b><br>
The parties’ pre-contractual exchanges of information can often be considerable. 
In the context of a major procurement exercise, there could be a Request for 
Proposals or Invitation to Tender, with responses and clarification.<br>
<br>
The best practice is to regard the final contract as being a stand-alone 
statement of the deal, rather than to refer to all of the pre-contractual 
documentation. This does, of course, mean that the parties do have to take care 
to incorporate into the final contract everything that is relevant. Traps that 
may arise include the following:<br>
<br>
1. The supplier, while in “selling” mode, will inevitably make grandiose claims 
– “the best in the world”. However, there will also be more specific statements 
of fact which should be set down either as warranties in the contract or as part 
of the definition of the services, particularly if they are measurable; and<br>
<br>
2. Conversely, the customer will have made a number of statements about its 
business requirements and the benefits it hopes to reap. If business benefits 
are expected, and can be measured and verified, they should be built into the 
contract (see risk allocation above).<br>
<br>
Only the relevant extracts of the supplier’s pre-tender documents should be 
used. For example, look at the following text:<br>
“The Supplier builds and develops Mobile Platform Software that enables mobile 
operators to offer mobile portal solutions.”<br>
<br>
With some minor changes it is turned into a clear obligation:<br>
“The Supplier shall builds and develops the Mobile Platform Software for the 
Customer that will enables the Customer to offer Mobile Portal Solutions.”<br>
<br>
Often suppliers will ask for letters of acceptance and attempt to bind a 
customer on these. It is important to use language such as “subject to contract” 
on communications with the other side until the final contract is signed, to 
prevent the other party asserting a contract exists when negotiations are still 
underway.<br>
<br>
In the next series, Saleem will discuss issues surrounding intellectual property 
rights in IT contracts and discuss in detail the specific components of an IT 
contract, more particularly service credit and service level mechanisms.<br>
<br>
Saleem Ashrafi Adam is a solicitor at Trowers and Hamlins and qualified in 
England and Wales. He has substantial experience advising on high value 
technology contracts and is currently seconded to Oman’s telecommunications 
mobile provider, Nawras. He can be contacted at
<a href="mailto:sadam@trowers.com">sadam@trowers.com</a>.<br>
