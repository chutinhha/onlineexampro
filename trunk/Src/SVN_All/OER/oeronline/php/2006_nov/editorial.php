    <p align="left" style="margin: 10" dir="ltr">
    <font size="4" face="Verdana"><b>Be a Leader, <br>
    Not a Manager</b></font><p align="justify" style="margin: 10" dir="ltr">
    <font size="1" face="Verdana"><br>
    Few days back while flipping through the pages of a business magazine 
    published from Dubai, I noticed a question sent by the CEO of a top-notch 
    company. The question read: “I slog for 14 hours a day and sacrifice most of 
    my weekends for the sake of my official duties. The company’s board of 
    directors appreciates my efforts. But the company’s performance is not up to 
    the mark and to make the matters worse, my wife is threatening divorce as 
    I’m not been able to devote enough time to the family.”<br>
    <br>
    We leave it to the CEO how he is going to handle his personal life but on 
    the office front, he seems to be a case of just a manager, not a leader. 
    This is not an isolated case as many top managers across markets are 
    grappling with this challenge. They must understand that as the head of the 
    organisation they should act like a leader and should not perform like a 
    manager. John Kotter, the renowned leadership and change management expert, 
    says, “Many organisations today are over managed and under led at least 
    partially because people have not been taught to appreciate the differences 
    between management and leadership. The differences are significant and the 
    implications for enterprise performance in today’s environment are most 
    powerful.”<br>
    <br>
    Often, we confuse management duties and personal style with leadership. 
    According to Kotter, leadership means setting a direction instead of 
    planning and budgeting; motivating people as opposed to controlling them and 
    problem solving; aligning people instead of organising and staffing and; 
    creating a culture of leadership. <br>
    <br>
    Going by what Kotter says, true leaders establish the vision for the future 
    and set the strategy for getting there. They motivate and inspire others to 
    go in the right direction and they, along with everyone else, sacrifice to 
    get there. They make sure that enough people understand the vision, but more 
    importantly, that they buy into it. Then they do whatever possible to create 
    conditions that will motivate their team to act on that vision.<br>
