    <font size="1" face="Verdana"><br>
    <br>
    “The management deals mostly with the status quo and leadership mostly with 
    change, the companies in the fast changing world are going to have to try to 
    become much more skilled at creating leaders.” As Oman moves further to make 
    its Vision 2020 a reality, the corporate sector needs to introspect on what 
    steps it has taken to create leaders. We can’t leave the task of creating 
    leaders to business management institutes alone. As a responsible corporate 
    citizen, the companies should invest their time and money to nurture 
    leadership within their respective ranks instead of confining it to one 
    larger than life individual who charms thousands.<br>
    With business groups in the Sultanate scaling up in size and transforming 
    into more complex entities, leadership should not be seen as a mysterious 
    quality reserved for a charismatic person at the top alone. Leadership sense 
    should be sparked among those also who are below him in the hierarchy and 
    assist him in accomplishing the leadership goals within their sphere of 
    activity.</font></td>
