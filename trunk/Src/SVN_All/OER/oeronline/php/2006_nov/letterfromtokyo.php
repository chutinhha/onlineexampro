<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana"><b><font style="font-size: 14pt">Rising Japan</font></b></font><font face="Verdana" size="1"><br>
<b>Whatever happens to the Japanese economy, there is a guaranteed supply of 
attractive investment opportunities in the Asian country, Veryan Allen reports</b></font></p>

<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="japan.jpg" width="400" height="276"></p>

<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<img border="0" src="pic66.jpg" align="right" hspace="5" width="101" height="133">Japan offers convincing evidence that stock markets do not necessarily go up 
over time. There were several “false dawn” recoveries during the 20 years since 
the Nikkei first exceeded its current level. Having been a good source of 
returns for value investors in recent years, most Japanese equities are no 
longer cheap by most metrics. Valuations are generally high and Japan is 
currently the most expensive major stock market despite being 60 per cent lower 
than in 1989. At current prices, even the most optimistic scenarios for the 
Japanese recovery could now be priced in. Many pundits claim that this time it 
is different with the deflation, bad debt and banking problems largely gone but 
a stronger economy may not necessarily mean a continued rise in the stock 
market.<br>
<br>
Japanese stocks have often attracted a lot of speculation. There are now 
numerous books and magazine articles on how individual investors can make<br>
<br>
a living by day trading. Almost every day there is an IPO that usually begins 
dealing at a very high premium to the offering price, regardless of the 
prospects for the underlying business. The Livedoor and Murakami scandals 
earlier this year caused a loss of confidence in many smaller stocks popular 
with individual investors. After the irrational exuberance of 2005, the smaller 
company space remains quite depressed. This year several Japanese 
fundamentally-based hedge funds have been shown to be simply leveraged beta 
funds rather than alpha generators. A rigorous investment process, sophisticated 
risk management, appropriate use of derivatives and flexibility of strategy have 
always been essential to consistent investment performance in Japan.<br>
<br>
Whatever scenario happens to the Japanese economy, there is a guaranteed supply 
of attractive investment opportunities. Purchasing good stocks and short selling 
bad stocks continues to be the best and safest investment strategy. Even during 
the worst years of the recession there were many companies doing well and today 
with the economy recovering, many firms are executing very poorly. Importantly 
for investors, shareholder activism and engagement with management are now 
commonplace in Japan. There have been numerous examples of successful value 
realization of assets of troubled Japanese companies. Managing for the sake of 
shareholders, focus on investor relations, efficient use of capital, corporate 
transparency and governance have all made significant progress in recent years. 
Many Japanese corporations are very reliable cash generators and returning this 
cash in the form of higher dividends and share buybacks is gaining traction. 
Private equity deals and management buyouts are occurring, as are strategic 
mergers and acquisitions.<br>
<br>
<img border="0" src="pic67.jpg" align="left" hspace="5" width="250" height="148">However, there are potential negatives on the horizon. The emergence of hostile 
takeovers has been met with corporate managements trying to protect themselves 
with poison pill and other defenses. The indebtedness of Japan may necessitate a 
rise in income and consumption taxes. Longer-term pension system is not in good 
shape for younger workers. The global geopolitical situation, in particular 
North Korea, could cause volatility and possibly spur Japan into increased 
defense spending. A new resurgence in commodities prices or a world recession 
would obviously damage the recovery. In the past, overseas economies were 
largely ignored. Now Japan follows the direction of global markets very closely.<br>
<br>
Real estate is perhaps the best benchmark for assessing the robustness of the 
Japanese economy. In the major cities land prices are firmly on an upward trend 
but now resort and even rural areas are following suit. The Tankan, a quarterly 
survey of business sentiment, remains in a positive trend and capital 
expenditures by small, medium and large firms are rising. The era of bad loans 
is over while new bank lending is up, as is consumer spending. A weak yen helps 
exporters and the currency is currently at its weakest since 1985. This should 
benefit major corporations like the car companies and the electronics firms. The 
Japanese government bond market has reacted to the economic growth with a rise 
in yields nearing 2 per cent. While this means an increase in cost of funds, it 
also shows that deflation has largely been defeated.<br>
<br>
<img border="0" src="pic68.jpg" align="right" hspace="5" width="175" height="250">Currently, there is a popular term in Japanese called “Rojinryoku,” which 
roughly translated means “senior citizen power”. The power of this demographic 
factor to fuel, rather than hinder, economic growth should not be 
underestimated. Many economists consider an aging society to be a negative 
development but they will likely be proven wrong. 2006 marks the first year that 
the post-war Japanese baby bombers start to retire, forming a massive consumer 
base with large personal assets, secure pensions and decades of free time. There 
is approximately USD 16 trillion in private savings in Japan, most of which has 
been sitting in zero interest bank accounts. The appetite to take some measured 
risk in the hope of higher returns is growing, as is the intention to spend.<br>
<br>
Businesses everywhere would be well advised to develop products and services of 
value to this important demographic factor. Hobbies, travel, renewed education, 
new skills, computer literacy, brain training and health services are all areas 
expanding rapidly. There will be many money-making opportunities in the 
post-industrial society that Japan has started to enter. The national 
penetration of mobile phones and internet usage is exceptionally high. A 
surprising fact is that more blogs are written in Japanese than English despite 
the much lower global population of Japanese speakers. Much of this blogging and 
social networking is conducted by older consumers.<br>
<br>
Japanese firms continue to innovate in many technological areas such as 
communications, robotics, optical networking, nanotechnology, alternative energy 
and transportation. While it is likely that large-scale manufacturing will 
continue to migrate to cheaper locations, product development, quality control 
and intellectual property will remain key strengths of the Japanese industry. 
The development and commercial use of new technologies remains a core component 
of Japanese economic growth. Early stage venture capital was simply not 
available during the 1990s in Japan. Now entrepreneurs with good business plans 
and strong management teams have access to varied sources of funding and the 
ability to go public to raise further capital.<br>
<br>
Japan is slowly emerging from a 17-year recession. The new Prime Minister, Abe, 
has recently replaced the maverick leader Koizumi whose reform program was 
detested by powerful vested interests and will need constant resolve if it is to 
continue. After nearly a decade of zero interest rates, the lending rate was 
raised to 0.25 per cent earlier this year. But interest rates are still very low 
and it is very unlikely the Bank of Japan will jeopardize the nascent recovery 
by a rapid series of rises. There will continue to be many opportunities in 
Japan for investors and businesses, provided it is gone about in the right way 
with the necessary level of commitment.<br>
<br>
Veryan Allen, a Honolulu-based Hedge Fund Manager, wrote this article after his 
recent trip to Japan.<br>
