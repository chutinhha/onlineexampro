            </font></b><font face="Verdana"><b><font style="font-size: 14pt">
            Omani Hinterlands Come Alive</font></b></font><font face="Verdana" size="1"><br>
            <b>The unique path-breaking initiative of Reach, Al Multaqa Al 
            Ramadani, brought together more than 50,000 people</b><br>
            <br>
            <img border="0" src="pic46.jpg" align="right" hspace="5" width="120" height="170">When Bhavneet Bhalla, Operations Manager, Reach, conceptualized Al 
            Multaqa Al Ramadani (AMAR) in June this year, he knew he was bracing 
            for a very different and unique kind of market activation project, 
            never attempted before at such scale in Oman. But at the end of a 
            month long exercise, he was surprised with the stupendous response 
            to the project from different quarters.<br>
            <br>
            Reach is the first of its kind market activation solutions provider 
            in the Sultanate that helps brands and marketers interact better 
            with people across Oman. Talking about AMAR, Rajeev Sinha, Assistant 
            General Manager, Reach said, “In our six months long market research 
            encompassing the interiors of the Sultanate, we found that there is 
            immense potential beyond the capital region. But the corporate 
            sector to a great extent focuses its energies on Muscat. We also 
            found that different communities within the interiors do not get 
            enough opportunities to come together. That’s how AMAR was 
            conceptualized. The model is based on bringing socially conscious 
            businesses, corporate houses and government agencies closer to 
            people in Oman’s interiors during Ramadan.” <br>
            As part of AMAR, more than 100 activities were organised in the five 
            key towns of Sohar, Nizwa, Sur, Ibri and Salalah simultaneously over 
            a period of 35 days starting from the first day of the Ramadan. 
            Reach tied up with community clubs and women’s forums to touch base 
            with the people. People of all age groups and genders participated 
            in the activities of their interests such as cultural events and 
            sports competitions, Eid festival celebration etc. AMAR’s most 
            popular activities included Arabic theatre, poetry nights, football 
            games, chess competition, tug-of-war, cooking classes, first aid and 
            road safety clinics, health &amp; hygiene workshops, civil defence 
            classes, Holy Quran recitations, Qaranqasho sessions, cooking 
            classes, management &amp; personality development workshop, Iftar 
            celebrations, symposiums and internet jamboree. <br>
            <br>
            Recalling the most interesting moments from AMAR, Bhalla said, “The 
            cultural show specially organised by the physically challenged 
            touched the hearts of the people. AMAR also attempted to revive the 
            traditional Omani games, which are losing out to the modern day 
            mobile and computer games. Large number of people thronged the 
            venues to participate and watch those games.” <br>
            <br>
            The local government bodies and senior administrative management 
            backed and appreciated the AMAR initiative. Sheikh Abdullah bin Said 
            As’ Saifi, member of Majlis A’Shura, Nizwa said, “Reach in 
            association with the Nizwa club did a commendable effort by 
            spreading the teachings of Islam especially among the kids. The 
            diverse range of social and cultural activities undertaken during 
            the Ramadan period helped in strengthening the culture and tradition 
            of Oman.” <br>
            <br>
            Mohammed bin Ahmad bin Said Al Busaidi, Walli of Sohar added, “We 
            are pleased with Reach’s effort to bring the private sector and 
            government machinery together on a common platform to serve the 
            interests of the public at large.” It must have been a great 
            challenge for Reach to manage the logistics for organizing a 
            programme of such nature and scale! “Indeed it was,” said Sinha. 
            “Our core team constantly crisscrossed the Sultanate to make sure 
            that all arrangements are in place. Each town was supervised by a 
            hub manager 24x7. We got great support from the local community 
            clubs, government authorities and our community partners Mazda, LG 
            and Al Mar’a (Regional) along with supporting partners Muscat 
            Pharmacy &amp; Stores LLC and Pizza Hut.”<br>
            <br>
            Did it make sense for the sponsors to invest mega bucks in AMAR 
            targeting the interior consumer market, at times of heightened 
            institutional demand? “For LG, to partner an activity of this nature 
            during the holy month was part of our corporate social 
            responsibility. LG firmly believes in investing back into the local 
            community and supporting the local tradition and culture. On the 
            business front, it is wrong to say that interior market is not so 
            important for us. Take for example, 78 per cent of the Omani youth 
            lives outside the capital region. Our brand is known for the latest 
            technology and path breaking products. Globally, youth segment 
            endorses the modern technology by adopting the most recently 
            launched products. So ignore the interior market at your own peril,” 
            said Anurag Guglani, General Manager, LG.<br>
            <br>
            Reach plans to return next year with an enhanced version of AMAR. 
            Sinha informed, “In 2007, we endeavour to launch AMAR in more than a 
            dozen locations and reach out to a far greater number of people. 
            Impressed with the success of AMAR, various government authorities, 
            communities clubs and brand marketers have approached us for forging 
            partnership for the next year. Apart from AMAR, Reach is currently 
            working on launching a series of community activities during the 
            first half of 2007.” </font></p>
