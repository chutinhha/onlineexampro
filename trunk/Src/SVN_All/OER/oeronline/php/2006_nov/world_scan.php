<br>
<b>Apple blames Microsoft for virus in iPods</b><br>
<img border="0" src="pic72.jpg" align="left" hspace="5" vspace="2" width="175" height="112">Apple apologised recently for shipping video iPods containing the Windows virus 
RavMonE.exe, which apparently made its way onto a small number of the ubiquitous 
devices at a manufacturing plant. Around one per cent of the units shipped after 
September 12, 2006, are affected. <br>
RavMonE.exe is a mass storage virus that only affects Windows computers. 
According to anti-virus vendors, which dub the malware Win32.RJump.a and Troj/Bdoor-DIJ, 
the virus is a Trojan that opens links to websites and allows others access to a 
computer. After installation, the Trojan contacts several remote sites to report 
the infection and availability of the backdoor, according to security firm 
Sophos, which rates it as a low risk infection. All up-to-date anti-virus 
applications should detect and remove the virus.<br>
“The iPod nano, iPod shuffle and Mac OS X are not affected, and all Video iPods 
now shipping are virus free,” Apple said in a statement on its support site. The 
company also took the opportunity to blast Microsoft’s Windows operating system 
for not doing more to protect customers from such malware. “As you might 
imagine, we are upset at Windows for not being more hardy against such viruses, 
and even more upset with ourselves for not catching it,” Apple said.</font><hr color="#C0C0C0" width="80%" size="1">

