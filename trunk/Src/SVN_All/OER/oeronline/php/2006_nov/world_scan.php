<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b>Coke, Nestle to launch calorie-burning beverage</b><br>
<img border="0" src="pic71.jpg" align="right" hspace="5" width="125" height="127">Coca-Cola and Nestle, through their joint venture Beverage Partners Worldwide, 
are expected to launch in November a carbonated beverage that burns calories. 
Branded as Enviga, a sparkling green-tea drink under the Nestea name, it is 
claimed to be ‘calorie deficient’ rather than calorie-packed. It uses a blend of 
green-tea extracts known for boosting metabolism called Epigallocatechin gallate 
(EGCG). Enviga is expected to have twice the EGCG as leading competitors, no 
more caffeine than an average cup of coffee and 20 per cent of the daily value 
of calcium for men and women ages 19 to 50. <br>
<br>
“There’s calorie, low-calorie, zero-calorie, and now we offer consumers 
negative-calorie products,” said Jason Warner, Group Marketing Director for 
Beverage Partners Worldwide Americas, which has worked on the brand for the past 
three-and-a-half years. Enviga will be introduced in berry, peach and green-tea 
flavors (a citrus flavor is also slated for some regional markets). Currently, 
it is under test stage in Australia, Canada, Mexico and eight markets in Europe 
for an eventual global rollout.</font><hr color="#C0C0C0" width="80%" size="1">

<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<br>
<b>Google acquires YouTube to deal a blow to Yahoo</b><br>
Google reshaped the landscape of search, grabbing YouTube in a US$1.65 billion 
stock deal. The deal is a blow to Yahoo, which has been struggling to catch up 
with Google in advertising — and has had little luck with its own online video 
experiments. <br>
<br>
Google CEO Eric Schmidt said the advent of video was the next step in the 
evolution of the Internet, and YouTube brand had great value for users, the 
company, and advertisers. Indeed, the startup that’s held legions captivated by 
quirky videos may be the next story of entertainment delivered via Internet, if 
only it can steer clear of legal landmines, and may hold the ticket to draw 
advertisers seeking richer media forms.</font><hr color="#C0C0C0" width="80%" size="1">

