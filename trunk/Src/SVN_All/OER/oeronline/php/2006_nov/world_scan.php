<b> <br>
Indians set to dominate global steel</b><br>
Three of the top 10 global steel companies are set to fall into the hands of 
Indians with the Tata group taking over the Anglo-Dutch firm Corus at a cost of 
US$7.6 billion. A merger of Corus with Tata Steel will create the sixth largest 
steel producer in the world and would amount to the biggest takeover of a 
foreign firm by an Indian company. The Anglo-Dutch company is currently ranked 
the ninth largest steel producer in the world while Tata is ranked 56th and the 
merger will more than and quadruple their sales to US$23 billion. Corus has 
24,000 employees in Britain and 16,000 in Holland. <br>
<br>
At present, Lakshmi Nivas Mittal is the undisputed king of steel manufacturing 
with his Mittal Steel being the world’s largest manufacturer of the commodity, 
followed by Arcelor, which he took over earlier this year.<br>
<br>
In fact, for Tata Steel also, this will be the third takeover of a steel company 
abroad — having acquired Millennium Steel of Thailand and Singapore-based 
NatSteel for US$553 million to gain access in the Asia Pacific region.<br>
