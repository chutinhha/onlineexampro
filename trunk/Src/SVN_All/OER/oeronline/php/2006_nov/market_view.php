<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><b><br>
</b><font face="Verdana"><b><font style="font-size: 14pt">Solid Gains </font>
</b></font><font face="Verdana" size="1"><br>
<b>With most of the other Gulf stock markets registering negative growth, MSM 
has defied the trend by recording the maximum growth in the region</b><br>
<br>
Happy days are back again! Muscat Securities Market (MSM) 30 index once again 
witnessed a significant increase in its value over the last four weeks (Sept 21 
– Oct 22, 2006), having risen by 7.22 per cent to reach an all-time high level 
of 5,715.76 points. The three sector indices experienced substantial gains in 
value as well, in addition to all of them showing much higher levels compared to 
the beginning of the year. Industry stocks recorded a growth of 14.49 per cent 
whereas services had 8.86 per cent and banking 6.85 per cent. <br>
<br>
<b>Top Gainers and Losers</b><br>
The five companies that topped the list of gainers were Oman Cables Industry (up 
47.04 per cent), National Detergents (35.38 per cent), Construction Materials 
(up 29.10 per cent), National Aluminium Products (up 26.32 per cent) and Majan 
Glass (up 24.30 per cent).<br>
<br>
Conversely, the five companies whose share value witnessed the biggest drops 
over the past four weeks were National Rice (down 20 per cent), FINCORP (down 
11.94 per cent), Oman Flour Mills (down 11.18 per cent), National Securities 
(down 9.21 per cent) and United Finance (down 4.69 per cent).<br>
<br>
<b>Volume and Turnover Toppers </b><br>
The most actively traded shares in terms of volumes this month were Alliance 
Housing Bank, Bank Muscat, Oman United Insurance, Dhofar International 
Development and Investment Holding Company, and Bank Dhofar. In terms of 
turnover however, Bank Muscat, Omantel and Alliance Housing Bank topped the 
charts, being the only three companies with traded volumes exceeding RO 5 
million.</font></p>

