<b>GCC Market</b><br>
From the tables on GCC Market Performance, we find that the performance of 
markets across the GCC region was varied in terms of their respective index 
values. While the Muscat Securities Market, the Kuwait Stock Exchange and the 
Dubai Financial Market witnessed monthly growths of 7.22 per cent, 5.29 per cent 
and 0.94 per cent respectively, the Tadawul All-Share Index (Saudi Arabia), the 
Doha Securities Market, the Abu Dhabi Securities Market and the Bahrain Stock 
Exchange experienced drops in the value of their respective indices by 5.07 per 
cent, 4.95 per cent, 3.06 per cent and 1.33 per cent, respectively.<br>
<br>
If we look at the returns so far this year starting from January 1, MSM is the 
only stock exchange in the Gulf region which has given handsome returns to the 
shareholders in percentage growth terms. MSM has grown by a smart 17.08 per cent 
whereas BSE (Bahrain) has achieved just 1.69 per cent growth. Rests of the stock 
exchanges have recorded very high negative growth rates.</font></p>

