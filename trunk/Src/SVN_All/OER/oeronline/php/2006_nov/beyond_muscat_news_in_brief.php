            AIRPORT CONSULTANCY DEAL</font></b><font face="Verdana" size="1"><br>
            The prominent engineering consultancy company, Hamza Associates in 
            Egypt, has won the contract to provide consultancy services linked 
            to the design and construction of an international airport in Sohar. 
            The contract is valued at RO2.3 million. The proposed airport 
            facility is expected to expand transport infrastructure in and out 
            of the wilayat to support the development of huge industrial port 
            and petrochemicals hub at Sohar.</font></p>
            <hr color="#C0C0C0" width="80%" size="1">
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1"><b>SBG AWARDS US$120 MILLION CONTRACT</b><br>
            Saud Bahwan Group has contracted Larsen and Toubro (Oman) to build a 
            residential township in Sohar at a cost of US$120 million. The 
            development will have nine buildings with a total of 288 
            two-and-three-bedroom apartments, and 149 three-and-four-bedroom 
            villas. Properties are expected to be ready by mid-2008.</font></p>
            <hr color="#C0C0C0" width="80%" size="1">
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1"><b>OMAN CONTAINER TERMINAL OPENS</b><br>
            <img border="0" src="pic59.jpg" align="right" hspace="5" width="200" height="141">The Oman International Container Terminal (OICT) at Sohar Industrial 
            Port has started operations ahead of schedule. The project is a 
            joint venture between Hutchison Port Holdings Group, Oman’s 
            Government, Netherlands-based Steinweg and Omani investors. The 
            first phase includes a 285 metres long quay, equipped with four 
            Post-Panamax quay cranes and eight gantry cranes. The next phase is 
            due to be ready in February 2007. When complete, OICT will have a 
            total quay length of 520 metres and a total yard area of 28 
            hectares. OICT facilities include a gatehouse complex, engineering 
            workshop, a customs inspection area and an administrative building.<br>
            Strategically located outside the Strait of Hormuz, OICT allows 
            vessels to avoid the higher premiums associated once inside Gulf 
            waters, thus potentially reducing shippers’ costs. Furthermore, 
            OICT’s ability to deliver a swift and efficient service to the 
            growing number of mega-vessels strengthens its port of choice 
            reputation amongst the world’s top shipping lines. </font></p>
            <hr color="#C0C0C0" width="80%" size="1">
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1"><b>OMANTEL WLL SERVICE IN 45 VILLAGES</b><br>
            More than 45 rural villages in several governorates and regions in 
            the Sultanate now benefit from wireless fixed line service known as 
            Wireless Local Loop (WLL) provided by Oman Telecommunications 
            Company (Omantel). The telecom major plans to connect more than 200 
            villages with the service. The Chinese company Huwaei has been 
            contracted by Omantel to implement the WLL project. <br>
            <br>
            Upon the completion of Wireless Local Loop project, Omantel will 
            cover all rural villages in the Sultanate with fixed lines and all 
            houses will enjoy the additional services including navigating the 
            internet, fax services, voice mail and other benefits. The number of 
            subscribers has so far reached 3,000. <br>
            <br>
            At the beginning of the project, the service was introduced in 25 
            villages in the Dhofar Governorate namely, Sahl Atin, Titam, Hageef, 
            Nahiz Lisan, Al Mughsail, Hafouf, Deem, Argoot Ashkhreet, Ashor, 
            Sha’t, Hasek, Sharbathat, Hadbin, Sahl Ashor, Tewi A’teer, Darbat, 
            Madinat Al Haq, Gabgat, Hoqab Mountain, Shlim, Al Showimieh, Al 
            Maziyonah, Shihab Aseeb and Rakhyut. <br>
            <br>
            Omantel had earlier introduced the service in six rural villages in 
            Muscat namely: Gahlout in the wilayat of Al Amerat, Al Habobiah, Sia, 
            Fans and Dabab in Quriyat and Al Sifah. <br>
            <br>
            The company has also introduced the Wireless Local Loop service in 
            several villages in Al Sharqiyah region including Burzaman, Al Aflaj, 
            Al Oyoun, Al Khatm and Al Hail in Al Mudhaibi, the villages of Dima 
            and Al Taieen, Ras Al Hamrah, Dabrah/Al Qafsah, Al Qutaifi (Ouf) in 
            the wilayat of Dima and Al Taieen and the villages of Asilah and Al 
            Jawabi in the wilayat of Ja’alan Bani Bu Ali. </font></p>
            <hr color="#C0C0C0" width="80%" size="1">
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1"><b>AROMATICS OMAN TO PRODUCE PX &amp; 
            BENZENE </b><br>
            Alfa Laval AB has received an order for their Alfa Laval Packinox 
            custom-designed heat exchangers for delivery to the Sohar Industrial 
            Estate in Oman. The order is valued at about RO3.5 million and 
            delivery will take place in 2008. The equipment will be placed in an 
            aromatics factory owned by the Aromatics Oman Limited at the Sohar 
            Industrial Estate which will be using state-of-the-art technology to 
            produce Paraxylene (PX) and Benzene.<br>
            <br>
            Paraxylene is a key raw material in the production of polyester 
            fibres and PET plastic bottles whereas Benzene is an industrial 
            chemical used to produce a wide range of plastics and other 
            chemicals. <br>
            <br>
            The project in Oman is an initiative to promote and develop projects 
            in Oman that serve a wider range of local investments and provide 
            employment opportunities. Initiated by the Oman Oil Company, these 
            projects are aimed to transform the country from a crude oil and LNG 
            producer to a manufacturer of petrochemicals. <br>
            <br>
            Alfa Laval received the order through the GS Engineering &amp; 
            Construction Corporation, a Korean company involved in civil, 
            architectural and environmental engineering. <br>
            <br>
            Alfa Laval is a leading global provider of specialized products and 
            engineering solutions based on its key technologies of heat 
            transfer, separation and fluid handling. The company’s equipment, 
            systems and services are dedicated to assisting customers in 
            optimizing the performance of their processes.</font></p>
