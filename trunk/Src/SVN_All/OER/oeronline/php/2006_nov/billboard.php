        <p align="justify"><font face="Verdana" size="1"><b>British Airways’ 
        links Oman to Canada’s Calgary</b><br>
        <img border="0" src="pic23.jpg" align="left" hspace="5" vspace="3" width="125" height="73">The GCC is going to have a new air link with Canada’s emerging energy 
        province of Alberta with the launch, in December, of a five-times weekly 
        British Airways’ service from London Heathrow to Calgary — the gateway 
        to the Canadian Rockies. “The oil and gas industry is one of the largest 
        employers in the GCC and its executives can now link into the Calgary 
        service with a same-day connection at Heathrow via British Airways’ 
        direct flights from the UAE, Qatar, Bahrain, Kuwait and Oman,” said the 
        airline’s commercial manager for the Middle East and Pakistan, Paul 
        Starrs. Calgary will join the award-winning airline’s global network of 
        140 destinations when the new service comes into operation on December 
        1. With seven weekly flights this winter from Muscat to London Heathrow, 
        travellers can conveniently link into the Calgary route.</font></p>
