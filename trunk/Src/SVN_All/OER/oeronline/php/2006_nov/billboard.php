        <p align="justify"><font face="Verdana" size="1"><b>Oman Mobile’s novel 
        charity initiative</b><br>
        <img border="0" src="pic20.jpg" align="right" hspace="5" width="100" height="38">For the third consecutive year, Oman Mobile in collaboration with Oman 
        Charitable Organisation, has contributed to the welfare of society 
        during the holy month of Ramadan. To make Ramadan a pleasant season for 
        all categories of society, the programme has been implemented in 
        collaboration with Oman Charitable Organisation under the theme ‘Think 
        of your brother who has nothing to eat’. Oman Mobile subscribers can 
        take part in this programme by sending SMS worth 300 biasas to 90001. 
        The full amount will be transferred, without any deductions, to Oman 
        Charitable Organisation. Oman Mobile’s keenness in conducting a number 
        of social programmes in the different wilayats of the Sultanate of Oman 
        is an example of the company’s noble mission and its initiatives to 
        ensure that social and human integration is maintained. Khalid Al-Kharousi, 
        manager of branding and market communications, Oman Mobile said that the 
        initiatives emphasises Oman Mobile motto ‘Connected to you’. <br>
        <br>
        As part of its social programmes, Oman Mobile has initiated its third 
        charity initiative under the theme ‘Share your Blessing’. The programme 
        has been launched in association with Oman Charitable Organisation. The 
        programme is a simple indication of the interaction between Oman Mobile 
        and local communities. Oman Mobile is undertaking initiatives to collect 
        RO30,000 for charity purpose. <br>
        <br>
        With the advent of Ramadan, Oman mobile allocated 100 mobile phone sets, 
        with Hayyak package, to Oman Charitable Organisation to distribute among 
        needy families. The company has allocated a number of mobile phone sets 
        for needy families and those with special needs. As part of its 
        initiatives to serve local communicates, Oman Mobile has also provided a 
        large number of mobile phones for schools and other institutions that do 
        not have the fixed line service in remote areas. This initiative is 
        aimed at ensuring good communication between these organisations and 
        their local communities. The move undoubtedly will contribute to the 
        smooth running of work, improving performance and ensuring better 
        contact of such organisations with local communities. The move will also 
        have a positive impact on the social and economic development of the 
        local communities.</font></p>
