        <p align="justify"><font face="Verdana" size="1"><b>GEM’s sea cadet 
        programme</b><br>
        <img border="0" src="pic22.jpg" align="right" hspace="5" width="60" height="86">Gulf Energy Maritime (GEM), the Middle East’s largest independent, 
        commercial product tanker operator with the world’s most modern product 
        tanker fleet, has launched a unique sea cadet training programme within 
        the region aimed at encouraging UAE and Omani nationals to take up a 
        career on the ocean. Under the programme, GEM plans to train 
        navigational and engineering cadets to serve on its expanding fleet of 
        panamax oil product/easy chemical tankers. “Within only two years of 
        operation, GEM has established itself as a world class player in the 
        energy transportation segment with six tanker vessels already in service 
        and 13 new builds under construction,” explained Yusr Sultan, Chief 
        Executive Officer, GEM. Sea cadet applications are restricted to UAE and 
        Omani nationals, reflecting the joint venture partnership structure of 
        the company. GEM’s equity stakeholders include Dubai’s Emirates National 
        Oil Company (ENOC), Abu Dhabi’s International Petroleum Investment 
        Company (IPIC), Oman Oil Company (OOC) and Thales of France under the 
        UAE Offsets Programme. </font></p>
