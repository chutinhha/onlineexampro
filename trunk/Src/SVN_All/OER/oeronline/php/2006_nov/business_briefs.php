<font face="Verdana" size="1"><b>Renaissance subsidiary wins US$5.5 mn contract<br>
</b>Renaissance Services’ Abu Dhabi-based oil and gas subsidiary Adyard LLC has 
won a contract from China’s Sinochem to carry out the offshore package of a gas 
field development in the emirate of Umm Al Quwain. The US$5.5 million contract 
involves the fabrication, loading out and transportation of the jacket and 
topsides for an offshore platform. <br>
<br>
Adyard will carry out the work at its Abu Dhabi yard and completion of the 
project is scheduled for next year. The Umm Al Quwain field is shared between 
Sinochem’s subsidiary Atlantis Holding Norway AS and the Abu Dhabi 
government-owned Mubadalah Development Company under and agreement with Umm Al 
Quwain signed in December 1999. Combined with the Zora field — shared by the 
emirates of Sharjah and Ajman — it will produce 150 million cubic feet of gas 
per day in the first phase. Output is set to double at a later stage. <br>
<br>
Adyard is premier oil and gas fabrication and maintenance business with an 
80,000m² facility with over 300m of waterfront load out capability in Abu Dhabi. 
The company is currently completing investment projects to expand its facilities 
and waterfront capacity further.<b><br>
