<b>AECO prepares for Blue City construction</b><br>
AECO Development LLC, part of Turkey based ENKA Insaat ve Sanayi A.&#350;. and Greece 
based Elliniki Technodomiki Teb Group of Companies, has announced that it is 
preparing to mobilise its crews to commence operations for the construction of 
Phase 1 of Al-Madina Al-Zarqa – The Blue City.<br>
<b><br>
NBO home finance for The Wave</b><br>
The Wave has signed up National Bank of Oman to provide home mortgage loans for 
purchases of residential properties. NBO has become the first commercial bank to 
offer home financing at the development. The properties can be purchased by all 
nationalities.<br>
<br>
<b>AES Barka distributes cash dividend</b><br>
AES Barka is reducing the capital by 40-50 per cent to distribute a special cash 
dividend to the shareholders. The company is also generating RO19.26 million by 
refinancing a long-term loan.<br>
<br>
<b>Oman grants Lebanon US$50 million for arts centre </b> <br>
Oman has granted Lebanon US$50 million to build a Lebanese-Omani arts centre in 
Beirut’s central district. Several Arab Gulf states have made donations to 
Lebanon after the recent conflict between Israel and Hezbollah which caused 
damage estimated at US$3.5 billion.<br>
<br>
<b>Oman to participate in Gulf exhibition</b><br>
Ministry of Commerce and Industry (MOCI) will participate in the fourteenth 
Joint Gulf Exhibition to be held in Kuwait in December. The exhibition, opening 
on December 9, 2006, will last till December 18, 2006.<br>
<br>
<b>WJ Towell forms JV with Take Solutions </b> <br>
W J Towell &amp; Co LLC has formed a joint venture, Towell-Take Solutions LLC, to 
provide solutions centred around automated data capture based on barcode and 
RFID technology, Timics ERP, supply chain management, business process 
management, data warehousing and business intelligence. <b><br>
<br>
Commercial banks’ net profit crosses US$251.9 million
</b> <br>
As per Central Bank of Oman (CBO), the net profits of commercial banks in Oman 
have reached US$251.9 million in the first eight months of the year from $200 
million in the corresponding period of 2005. <br>
<br>
<b>BORL awards US$210 million contract</b><br>
Bharat Oman Refineries Ltd. (BORL), a joint venture of Bharat Petroleum 
Corporation Limited (India) and Oman Oil Company Limited, has awarded a contract 
worth US$210 million to an Indian company Bharat Heavy Electricals Limited (BHEL). 
It is for setting up a 99 MW captive power plant as part of its refinery project 
at Bina in Madhya Pradesh (India). <br>
<br>
<b>Oman in Seoul exhibition</b><br>
The Sultanate took part in the Middle East Civilisation and Islam Exhibition 
held at Seoul. The exhibition was organised to mark the golden occasion of Islam 
in Korea.<br>
<br>
<b>Moody’s upgrades Oman’s credit ratings</b><br>
Moody’s Investors Service has upgraded the long-term foreign and domestic 
currency government bond ratings of Oman to reflect the significant 
strengthening of its public and external finances. Moody’s assigned a long-term 
domestic currency government bond issuer rating of A3 with positive outlook to 
Oman. Previously, it was Baa1. The country ceiling for long-term foreign 
currency bonds was also upgraded with Oman’s rating moved up to A1 with positive 
outlook from A2.<b><br>
