DHL Oman enhances tracking service <br>
<img border="0" src="pic86.jpg" align="right" hspace="5" vspace="2" width="200" height="119"></b>The world leader in express and logistics, DHL, recently invested RO10,000 
into upgrading its wireless hand-held scanners for the company’s on-the-go 
couriers in Oman.<br>
<br>
DHL Oman Country Manager, Mark Benton said: “At DHL, we recognise the importance 
of timeliness and accuracy. With up-to-the-minute shipment information, we 
guarantee our customers total visibility and transparency so they can track and 
trace their packages at every step of the process with the new high-tech 
scanners.” <br>
<br>
Deployed with all DHL couriers in Oman, the new scanners allow barcode scanning 
and data entry via a keypad; download data automatically via the GSM phone 
network and work via GPRS network if available. With this resourceful one-piece 
communication device, DHL personnel on the field are connected to the DHL Global 
system to give visibility to both customer service staff and clients enabling a 
fast and efficient tracking service. The new scanners significantly reduce the 
time lag between capture and transmission of data from the field to the shipment 
record database. Customers can now obtain confirmation within five minutes of 
delivery as the new scanners provide instant availability of shipment 
information through DHL’s global network.</font><hr color="#C0C0C0" width="80%" size="1">

