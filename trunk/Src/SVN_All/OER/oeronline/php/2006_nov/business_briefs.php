NBO’s net profit jumps by 88 per cent<br>
</b>National Bank of Oman has made a net profit of RO20.5 million in the first 
nine months of 2006. This net profit for nine months surpasses the net profit of 
RO20.3 million which was achieved for the full year of 2005. The net profit for 
the first nine months of 2006 has increased by 88 per cent compared to the same 
period of 2005. <br>
<br>
NBO’s Chairman, Sheikh Suhail Bahwan, commented: “National Bank of Oman’s 
results for the first nine months of 2006 reflect growth across all our 
businesses. Total assets grew 31 per cent to RO984 million from RO750 million as 
at September 2005 and 13 per cent as compared to RO870 million as at December 
2005. Net loans and advances increased by RO142 million (26 per cent) to RO684 
million as compared to December 2005. Customer deposits increased in the same 
period by RO124 million (20 per cent) to RO740 million from RO616 million in 
December 2005. Net interest income grew by 15.5 per cent to RO25.7 million and 
non-interest income climbed by 70 per cent to RO13.1 million. Our business 
continues to expand, generating sustainable growth for our shareholders.”</font><hr color="#C0C0C0" width="80%" size="1">

<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>
<img border="0" src="pic87.jpg" align="left" hspace="5" width="175" height="70">Oman Air’s profit up by 7 per cent<br>
</b>Oman Aviation Services ‘ Oman Air’ net financial results for the first nine 
months of the year ended September 30, 2006 showed a profit of RO1.452 million 
compared to a profit of RO1.351 million during the same period in the previous 
year, an increase of RO101,000. HE Said Bin Hamdoon Al Harthy, Oman Air’s 
chairman said, “In airline business during the mentioned period, Oman Air 
carried 931,753 passengers, up 10 per cent over the previous year, with a seat 
factor of 75 per cent, and even achieved a marginal improvement in average 
yield. Airport Services business has reported improved financial performance 
with the increased flight movement (+ 2,312 flights or 15 per cent) and higher 
catering uplift (+ 699,180 meals or 28 per cent) during the first nine months of 
the year.” <br>
<br>
He added, “Oman Air recently signed lease agreements to acquire two new B 737 
800 aircraft in May 2007 and March 2008, one of which will replace an existing B 
737 700 aircraft. We will continue to explore new high potential routes such as 
Amman by the end of this year. We are also aiming to strengthen our air charter 
services in Oman. The company is working closely with Petroleum Development of 
Oman (PDO) to upgrade the air services to their oilfields with jet aircraft. 
Oman Air has also taken significant initiatives to enhance the quality of its 
product and services. During the year, e-ticketing has been introduced at most 
stations in the network. About 51 per cent of our passengers now use e-tickets. 
Internet booking has also been launched along with the airline’s new website. We 
have also introduced a new customer loyalty programme, Sindbad.”<br>
