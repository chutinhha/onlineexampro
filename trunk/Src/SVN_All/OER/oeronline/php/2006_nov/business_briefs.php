<b>OCIPED delegation to Iran</b><br>
A number of Omani businessmen went to Tehran as part of OCIPED’s business 
delegation in September to promote the exports of Omani products to Iran.<br>
<br>
<b>Nawras relaunches website </b> <br>
Nawras has relaunched its bi-lingual website introducing a totally new look and 
a host of new features. It is designed to showcase the company’s services, 
values and news. Full details of all products, prices, coverage, store locations 
and promotions are featured on the website.<br>
