</b></font><b><font style="font-size: 14pt" face="Verdana">Oil Prices: Risk 
Skewed to Upside?</font></b><font face="Verdana" size="1"><br>
<b>Even if oil prices fall to US$50, the GCC countries will still generate 
budget surpluses and act as net creditors to the international financial markets</b></font></p>

