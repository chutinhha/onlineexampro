      <p align="justify"><font face="Verdana" size="1">The decline of crude oil prices from their July high of US$78 has now 
re-triggered calls for an OPEC production cut amid unmistakable evidence of an 
oil glut. The IEA has reduced its estimates for global growth in oil demand as 
the housing bubble in the United States deflates and the risk of a slowdown in 
consumer spending, even a possible recession, is unmistakable.<br>
<br>
As the world’s largest consumer of crude oil and gas, America can be likened to 
an 800-pound gorilla in the bazaars of black gold. The reasons for a 25 per cent 
fall in prices are diverse. One, the Neocon hawks in the White House were forced 
to accept a UN-brokered ceasefire in Lebanon after Israel’s failure to defeat 
Hezbollah, meaning diplomacy, not war, would determine the resolution of Iran’s 
clandestine uranium enrichment program.</font></td>
      <td width="22%">
