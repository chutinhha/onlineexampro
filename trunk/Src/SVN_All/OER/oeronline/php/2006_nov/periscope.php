<p align="justify" dir="ltr" style="margin-right: 10px"><font face="Verdana" size="1">This failure to translate threats into a preemptive strike against Iran 
compressed the geopolitical risk premium that had added at least US$10 to crude 
in July, when Israeli warplanes bombed Beirut and there was risk of the war 
spreading in the Middle East.<br>
<br>
Two, US oil inventories expanded because of the end of the summer driving season 
and refinery shutdowns for maintenances, which normally triggers in softness in 
physical crude demand.<br>
<br>
Three, the collapse of the US energy hedge fund Amaranth, after US$ 7 billion in 
speculative losses in the natural gas derivatives markets, meant that hedge 
funds and Wall Street commodity prop desks reduced their exposure to oil prices.<br>
<br>
In essence, the leveraged daisy chains of hot money that had dominated open 
interest in New York Merc and London’s International Futures Exchange now become 
net sellers of West Texas Intermediate and North Sea Brent crude contracts. In 
essence, oil lost its bull market cheerleader of last resort.<br>
<br>
Four, the oil market underestimated the time with which British Petroleum was 
able to resume production in its Prudhoe Bay assets in Alaska. As BP resumed its 
Alaskan production, it was yet another nail in the coffin for the summer oil 
bulls.<br>
<br>
Five, hurricane season in the Gulf of Mexico was somewhat benign, unlike 2005, 
the year of Katrina, and wrecked offshore rigs off the coast of Louisiana, and 
Texas.<br>
<br>
Six, even though crude oil prices have fallen by no less than US$20 a barrel 
from their summer high, oil prices are still high in historic and nominal terms. 
After all, amid all the hysteria over “oil superspikes” on Wall Street, 
investors should remember that the price of crude oil was US$20 in 2002.<br>
<br>
I doubt if prices will plunge below US$ 50 as long as the US economy avoids 
recession. Chinese GDP growth is still extraordinarily high at 11 per cent and 
as China’s central and western provinces emulate the economic takeoff path of 
Guandong and Shanghai, its energy consumption (the world’s second largest after 
the US) should anchor global oil demand. It will offset a US slowdown, 
particularly as Japan and Europe are both accelerating growth.<br>
<br>
The IMF suggests that Chinese economic growth will average 9.5 per cent between 
2007 and 2011 and India, South Korea, ASEAN and Taiwan will combine to ensure 
three more years of strong Asian demand. I believe the market also 
underestimates Saudi Arabia’s determination to avoid a destructive bear spiral 
in oil prices, as the world witnessed in 1986 or 1998.<br>
<br>
As the proverbial central bank of oil, OPEC’s swing producer, Saudi Arabia can 
easily defend crude at US$60. The world is, after all, operating on a mere 2 
million spare capacity at a time when the threat of a US–Iran showdown has not 
ended, violence in Iraq and the Niger Delta continues and energy nationalism 
from Russia to Venezuela threatens to change the rules of the game in 
international oil and gas exploration/drilling.<br>
<br>
<b>Geopolitical Risks</b><br>
Geopolitical risk can flare up at any time, as North Korea proved with its 
recent test of a nuclear weapon. The possibility of sanctions against Iran, 
illustrated by the US Treasury’s blacklisting of Bank Saderat and squeeze on 
Tehran’s international banking relations, is simply not factored into an oil 
price below US$60. Moreover, a cold winter in the Western Hemisphere alone could 
lead to a bull run in oil prices as long as global oil–demand equations remain 
tight.<br>
<br>
As long as OPEC imposes compliance with its quota cuts, I believe the risk of 
crude oil and gas prices is skewed on the upside. The fate of oil prices are 
increasingly determined by demand trends in Asia and supply uncertainties in 
Nigeria, Russia, the Middle East and Venezuela, both secular global trends that 
simply will not go away.<br>
<br>
The fall in oil prices has prevented a bull run in the Saudi stock market, with 
the Tadawul index below 10,500. However, even if oil prices fall to US$50, the 
GCC states will still generate budget surpluses and act as net creditors to the 
international financial markets.<br>
<br>
While valuations in Saudi Arabia, Qatar and the UAE remain high by emerging 
market standards, Oman, Bahrain and Kuwait are still value players in the GCC, 
with several bellwether banks trading at a single digit multiple. As long as 
there is no panic selloff in oil prices, the Gulf stock market prospects will 
not be hostage to panic in black gold.<br>
<br>
<i>Matein Khalid is a renowned investment banker based in Dubai. His regular 
monthly column will focus on the economic happenings in the Gulf Cooperation 
Council member countries and their impact on the global finance.</i><br>
