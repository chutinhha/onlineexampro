<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><font face="Verdana"><b><font style="font-size: 14pt">‘Only Nokia 
Ahead of Us in ME’</font></b></font><font face="Verdana" size="1"><br>
<b>Akshay Bhatnagar caught up with Sung (Steve) Woo Han, Director – 
Telecommunication Division (MENA), Samsung Electronics Co. Ltd., when he visited 
Muscat recently to appoint Cellucom as Samsung’s second authorised distributors 
and retailers in the Sultanate</b><br>
<br>
<img border="0" src="pic69.jpg" align="right" hspace="5" width="140" height="176">Samsung Electronics is on an upswing. The US$56.7 billion Korean giant’s third 
quarter operating profits marked a 30 per cent increase over the second quarter 
this year. The Middle East and Africa market despite its small size has added to 
Samsung’s growth. Mobile handset segment is playing a major role in Samsung’s 
quest for the top. “Though the mobile phone market in Middle East and Africa is 
quite small, it is very crucial to our global business growth plan,” informed 
Sung (Steve) Woo Han, Director-Telecommunication Division (MENA), Samsung 
Electronics Co. Ltd, on the sidelines of a press meet in Muscat recently.<br>
<br>
Based at the company’s regional headquarters in Dubai for the last four years, 
Han keeps shuttling across the region with a view to enhancing business. “In the 
first half of this year we have clocked sale of 600,000 mobile handset units in 
the Gulf market, excluding Saudi Arabia, to achieve 22 per cent growth,” he 
said. As per estimates, Samsung’s strongest market in the Gulf (excluding Saudi 
Arabia) is in the UAE, where it commands 20 per cent share of the 2 
million-strong handset market. Though he refused to divulge the exact numbers, 
Han said Samsung enjoyed 12-13 per cent market share in Oman too. <br>
<br>
Samsung sold 7.5 million handsets in the Middle East and Africa (MEA) last year 
and aims to sell 10 million this year, expecting to earn US$1.7 billion. The 
company claims a market share of 17 per cent in the MEA region. Though Samsung 
has grown by leaps and bounds in the last few years, many markets across the 
region have reached saturation levels — with mobile penetration crossing the 80 
per cent mark. So, how does Samsung plan to meet its target?<br>
<br>
Han replied, “I do agree that many countries in the MEA region are not going to 
grow at the rate seen in the last few years. But that is not going to stop us 
from meeting our objectives. Every month, we are launching new models with path 
breaking features to woo the replacement segment. We are appointing new 
distributors and retailers across markets to increase our reach. We are 
partnering with mobile service operators, both existing as well as new entrants, 
to bundle our handsets. Our aggressive marketing promotion is ensuring 
visibility as well as sales for our product range. Many African countries with 
low mobile penetration are opening up markets. This will lead to a massive jump 
in handset sales there.”<br>
<br>
But aren’t the other mobile brands doing the same thing? “True, but we have an 
edge over others when it comes to product features and marketing. That’s why we 
have climbed to the No.2 position, with only Nokia ahead of us in the Middle 
East,” Han claimed. <br>
<br>
As for the models, Samsung has launched three new mobile phones as part of its 
Ultra range series. The XD820 is the slimmest with 6.9 mm depth. Han added, “Our 
latest innovation is i750, a powerful pocket pc smart phone. It is Samsung’s 
first PDA with WLAN support, business card scanner and 2 mega pixel camera.” <br>
<br>
So, isn’t it time for Samsung to look at the MEA region as a base for an 
assembly line, if not an R&amp;D centre? “We could possibly look at this in the 
future. The current demand is met by our production units in Korea, China and 
India. As of now, we have no plans to extend our production base to the MEA,” 
Han revealed.</font></p>

