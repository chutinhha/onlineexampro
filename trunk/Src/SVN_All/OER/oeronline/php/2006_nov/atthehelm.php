            </font><font face="Verdana" style="font-size: 14pt"><b>Blueprint to 
            success<br>
            </b>
            </font><font face="Verdana" size="1"><b><br>
            From a one-man one-room start-up architect enterprise, Fuad Abdullah 
            Al Kindy has come a long way in the last 16 years. He’s now the 
            Managing Director of Ibn Khaldun-Almadaen Engineering Consultants. 
            In a freewheeling conversation with Abhijit Sinha, he spells out his 
            mantra of success.</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="atthehel.jpg" width="200" height="182" align="left" hspace="5" vspace="4">M</b></font><font face="Verdana" size="1">erger 
            was the trigger to our success. It came about in 1999, within three 
            months of parleys with my friend, a civil engineer colleague also 
            working with me in the Ministry of Agriculture. He too had his own 
            established office – Ibn Khaldun. Mine was Almadaen, set up in 1990. 
            As both of our companies were quite well known, we decided to retain 
            both names. We decided to merge as we assessed there were too many 
            engineering enterprises, most of them small and not of good quality. 
            We wanted to establish one of special quality.<br>
            <br>
            When we merged my firm had 10 people; together our strength was 14, 
            and soon went up to 20. The staff strength has grown six times – we 
            have 65 of them now. In the last seven years, the business volume 
            turnover has gone up by about 15 times. And this came about due to 
            three factors: our merger, the rapid growth of Oman’s economy and 
            our dedication. We are Omanis running this business ourselves, not 
            as sponsors or dealers, and doing all the work ourselves — meeting 
            every client and associates. So, our contribution is very tangible.<br>
            <br>
            From the beginning, I have been selective in my projects, preferring 
            not to go by numbers but by particular clients who are keen about 
            good design, who come to me because they appreciated my work. So, 
            it’s never been more projects for less fees for me. Rather, it was 
            the other way round – and I have managed to build a good name.<br>
            <br>
            Now, we have expanded again, mainly into transportation projects — 
            ports, airports and roads — as there’s a lack of specialist 
            consultants in Oman in these fields. It’s a new chapter for us. We 
            are associated with international consultants to deliver 
            professional work and to convince the clients involved in these 
            mega-projects.<br>
            <br>
            We are associated with international consultants for Sohar Port, and 
            with international associates for other projects — expansion of 
            airports, for royal flights and the Ministry of Defence aviation 
            projects. We started road projects with international associates 
            too, but have now reached a stage where we can do it on our own.<br>
            <br>
            In the beginning we were taking a lot of input from our 
            international associates and most of the work was done by them. But 
            we made it clear in our agreement that this was a transitional 
            system where we use these projects to acquire the know-how — to us 
            as a company and to our employees as Omani professionals. Now, quite 
            a few of them have set up their own firms. That’s our contribution 
            to the country.<br>
            <br>
            I am happy to see that we, Omanis, have been successful to a large 
            extent in cutting dependence on the inputs of the international 
            associates. I am not attributing all this to myself alone. Rather, 
            it’s due to an able management — an Omani management, professional 
            and capable, as required by clients.<br>
            <br>
            While studying in the US, I got to know of their strategies and 
            systems. Some of the courses were structured to expose us to that 
            work environment. But in management and architecture work, it’s not 
            so sharply defined — that is American and this is Omani. Enhancing 
            management in our company has been more by default, plus working 
            with the government and international associates. We also approach 
            other organisations that offer guidance — we attend courses, 
            seminars and conventions.<br>
            <br>
            We are exploring new fields, discussing within our management, 
            trying to cap some areas where we have over-exposure. There are too 
            many projects of the same type, some of them in Oman. We operate 
            within our limits, not wanting to stretch ourselves by going to 
            banks. From the financial aspect too, we feel there’s not much 
            justification to venture outside Oman now.<br>
            <br>
            We want to be known for our specialized knowledge and performance — 
            for our range of projects. My core strengths are stability and 
            happiness of my family, especially in taking care of my youngest 
            daughter who is autistic; and dedication and concentration in my 
            business.<br>
            <br>
            My advice to the young professionals is simple. Don’t take any 
            shortcuts, have a long-term vision, have patience and work hard.</font></p>
