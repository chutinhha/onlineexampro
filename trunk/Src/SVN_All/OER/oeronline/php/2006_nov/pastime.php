<b>Elaine Armstrong, Director of Business Development, Muscat Golf Course 
Project LLC, is as net savvy as she is on the turf, finds Anne Kurien</b><br>
<br>
<img border="0" src="pic36.jpg" align="right" hspace="5" vspace="2" width="150" height="266">It’s been less than a year since she arrived in Oman. Yet Elaine Armstrong is a 
veteran here — so well she knows the region now. How did this happen, you might 
ask. “Watch out, Muscat. I am going to be there everywhere in my 4WD!” pat comes 
Elaine’s declaration. <br>
<br>
Earlier, Elaine worked in Abu Dhabi where she lived for almost three years. 
Someone who loves challenges and taking on new projects, she came to Oman when 
she was given the responsibility of getting the Muscat Golf Course Project LLC 
started, as the Director of Business Development.<br>
<br>
“I love playing golf and since I work in the same field, I make it a point to 
keep myself abreast of all that’s happening in the world of golf in the 
international arena. I surf almost everyday and read up everything from daily 
golf news to the news on new players in the circuit to tournaments and the new 
golf equipment that’s used or launched,” reveals Elaine. <br>
<br>
But it’s not just golf that keeps Elaine hooked to the Internet once she comes 
back home after work. An avid Bridge player, Elaine loves to test out her card 
game skills on the net. “I love playing Bridge on the Internet. I get to meet 
many people from around the world too as I take part in the Bridge Tournament,” 
she says. “Ideally, I would like to play Bridge with enthusiasts in Oman but 
most of them are homemakers and they hold tournaments in the mornings when I am 
at work.” <br>
<br>
Elaine makes it a point to give into her passion for the game at least five 
times a week! And on a day she has ample time in hand after work and the bridge 
tournament, Elaine likes to cook dinner for herself. Elaine, who is single, 
loves to experiment with cooking and trying out new recipes. “I love cooking 
Indian food and enjoy experimenting with the spices. The results are usually 
good though I tend to forget the combination I use every time,” she says 
laughing. Cooking, in fact, is a way of relaxation for Elaine. “On Fridays, if I 
am in the mood, I like to pamper myself with a nice, lavish dinner that I have 
cooked.”<br>
<br>
Traveling and exploring new locales is yet another passion of Elaine’s. 
“Earlier, when I was in Abu Dhabi and even in Canada, I would get on to a flight 
and head for some country I had not seen. In fact, if I didn’t travel I would 
get withdrawal symptoms,” she says jokingly. After coming to Oman Elaine has not 
taken any holidays yet, though she did visit her daughter in England. “I do make 
it a point to go to England at least twice a year. I will be making a trip to 
Prague towards the end of this year on one of my destination trips.”<br>
<br>
Elaine has not been able to explore much of Oman in the summer but with the 
cooler days approaching, she just can’t wait to get started. “I have only been 
to Al Sawadi, Bandar Jissah, Sohar and Nizwa. I absolutely love the landscape of 
Oman. It is so beautiful. And now as the climate is getting cooler, I will be 
making up for all the exploring I missed out so far,” she declares.</font></p>
