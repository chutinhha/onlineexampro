            Mobile Games &amp; Tourism — Stratford Unplugged</font></b></font></p>
            <div align="center">
              <center>
              <table border="0" cellpadding="0" cellspacing="0" style="border-collapse: collapse" bordercolor="#111111" width="96%" id="AutoNumber2">
                <tr>
                  <td width="59%">
                  <p align="justify"><font face="Verdana" size="1">
                  We live in a globalised “Attention” economy in which the key 
            challenge is customer engagement and retention. E-Games, through 
            their popularity with a wide spectrum of users and age groups, have 
            demonstrated a potential to engage consumers and their spending 
            power. As the converging digital media and communications 
            technologies have evolved to embrace very high speed broadband and 
            wireless connectivity as well as ever improving graphics displays 
            and emerging Global Positioning (GPS) applications, the mobile phone 
            and PDA have become practicable and attractive vehicles for 
            sophisticated marketing communications.</font></td>
                  <td width="41%">
                  <p align="center"><font face="Verdana" size="1">
            <img border="0" src="pic40.jpg" hspace="5" vspace="2" width="100" height="122"><br>
                  David Wortley<br>
                  Director, Serious Games <br>
                  Institute, Coventry University and a presenter at eGames 2006</font></td>
                </tr>
              </table>
              </center>
            </div>
            <p align="justify" style="margin-left: 5px; margin-right: 10px"><font face="Verdana" size="1">
            Somewhat ironically, in an age where international brands such as 
            McDonalds and KFC have made shopping malls across the world look 
            almost identical, the notion of “place” is becoming an important 
            differentiator in attracting business. Indeed, sustainable 
            development will to a very large extent depend upon the ability of 
            villages, towns and cities to attract and retain customers.<br>
            <br>
            Tourism is stimulated by the attractiveness and accessibility of 
            location and a mixture of local ambience, facilities, desirability 
            and culture. Can mobile devices and e-games technologies play a role 
            in supporting tourism?<br>
            <br>
            At Coventry University there is a pilot project designed to use 
            mobile devices such as PDAs and phones to stimulate tourism and make 
            towns and cities more accessible and appealing. One such example is 
            “Stratford Unplugged” which aims to provide tourists with 
            information at any time, any place and in any form and its services 
            include:<br>
            <br>
            &#9679;
            Where am I and what’s near me?<br>
            &#9679;
            Did you know that? (Tourist information and intelligent travel 
            guide).<br>
            &#9679;
            Information services (news and weather).<br>
            &#9679;
            Electronic coupons redeemable at local shops &amp; local advertising.<br>
            &#9679;
            Where is the nearest … ?<br>
            <br>
            Stratford upon Avon is an ideal location for such a trial as it is 
            already a tourist attraction probably best known as the home of 
            William Shakespeare and the Royal Shakespeare Company. The use of 
            mobile devices adds value to the tourist proposition and supports 
            local trade. The Stratford Unplugged project is part of the West 
            Midlands Mobile and Wireless (WMMW) project to support the 
            development and application of mobile and wireless technologies.<br>
            <br>
            E-games applications which use location based services such as 
            Stratford Unplugged could have huge potential to not only enhance 
            the tourist’s experience in an innovative way, but also attract 
            additional tourists and potential revenue streams. Elsewhere in the 
            Midlands of the UK, PDAs have been used for location based 
            multi-player games in which participants interact in a virtual world 
            which mirrors their physical location. In one Nottingham-based 
            project designed to stimulate healthy exercise, participants moved 
            about the city with the PDA monitoring both position and player 
            heart rate and competed with other players in a game which improved 
            both their health and knowledge of the city.<br>
            In Stratford, PDAs can be hired by tourists for RO5 per day from a 
            local hotel or tourist information centre and local retailers are 
            being encouraged to considering becoming hosts to Wireless “Hot 
            Spots” which not only could potentially benefit the retailer, but 
            also the whole of the town as its modern facilities are enhanced by 
            a service which the whole community can buy into.</font></p>
