				</font></b><b><font face="Verdana" size="4">JAZZ IT UP</font></b><font face="Verdana" size="1"><br>
                <b>The jazz has always been a success story for Honda ever since 
                its introduction. Malcolm Xavier Crasta tested the latest 
                version to see if it lives up to the name of its predecessor<br>
                </b><br>
                <img border="0" src="autotalk.jpg" align="left" hspace="8" vspace="6" width="350" height="226">Honda has followed-up one of 
                the most successful models in supermini history with its all-new 
                Jazz. At a time of rising fuel prices and financial instability, 
                a range of low emission, high economy petrol engines makes the 
                new model an attractive option for the increasing number of 
                motorists looking to downsize. By enhancing its strongest 
                qualities, Honda engineers have taken the innovative design and 
                versatility of the current Jazz to the next stage. Greater 
                flexibility, more interior space and lively, but economical 
                engines raise the bar even further in a class that’s struggled 
                to match the Jazz for customer satisfaction and reliability.<br>
                <br>
                <b>Hard act to follows<br>
                </b>The hugely-popular Jazz was always going to be a hard act to 
                follow – simply because of the resounding success of the current 
                car. As well as attracting consistently strong demand from 
                customers in every year since its 2002 launch, the Jazz has also 
                won over 30 industry awards, making it one of the most 
                celebrated models ever. The sequel builds on this success, with 
                a more stylish, contemporary exterior design to appeal to a 
                broader audience. A slightly larger body style brings better 
                visibility and means Jazz is even more spacious.<br>
                <br>
                <img border="0" src="autotalk1.jpg" align="right" hspace="8" vspace="6" width="163" height="197">Ride comfort has been much improved through a number of 
                suspension revisions, which contribute to the ‘big car feel’ of 
                the new Jazz. Meanwhile, a longer wheelbase (by 50mm) and wider 
                front track (by 35mm) give the car greater agility. Honda’s 
                stability assist, VSA, is now available across the range. The 
                overall height of Jazz remains the same (1,525mm) but the length 
                of the car has increased slightly by 55mm (to 3,900mm) and it is 
                also slightly wider, by 20mm (to 1,695mm). Getting in and out is 
                now much easier, thanks to wider-opening rear doors which open 
                in three steps – just like their front counterparts – for added 
                convenience in tight parking spots. Stopping ability has been 
                increased with larger brake front callipers, and the brake pedal 
                has been tweaked to improve feel. Front ventilated discs and 
                rear discs are matched with ABS, EBD and Brake Assist.<br>
                <br>
                Once inside, the cabin is now even more spacious for driver and 
                passengers, with greater headroom and an improved driving 
                position. The slightly increased exterior length and width have 
                helped improve passenger space, as has the ‘pushing forward’ of 
                the windscreen. Rear seat passengers now have 37mm greater 
                knee-room, while the distance between front and rear passengers 
                is up by 30mm.<br>
                <br>
                <img border="0" src="autotalk2.jpg" align="left" hspace="8" vspace="6" width="408" height="186">Visibility gets a considerable boost, too. Reduced width 
                A-pillars, a larger windscreen and quarter windows three times 
                the size of those in the previous model make for more relaxed 
                driving, while retractable rear headrests ensure the view out of 
                the rear window is now totally unobscured. Increasing the 
                feeling of spaciousness and freedom, EX models are equipped with 
                a panorama roof that extends over the rear seats for an 
                expansive sky view. Heat absorbing glass and a power sun shade 
                ensure a comfortable cabin environment. A single CD/radio is 
                integrated into the fascia, offering MP3/WMA playback and 
                speed-dependent volume adjustment. a USB adapter is located in 
                the centre console storage box, which enables fifth generation 
                iPods and a variety of other portable music devices to be played 
                via the Jazz’s audio system.<br>
                <br>
                <b>Strong architecture<br>
                </b>The new Jazz benefits from Honda’s ACE body structure, a 
                concept that is being progressively rolled out across the Honda 
                range. In particular a front polygonal main frame helps to 
                prevent misalignment between vehicles of different sizes and 
                construction and multiple energy absorbing pathways disperse 
                impact energy to prevent cabin deformation. Dual front and side 
                airbags are standard on all models, as are full length side 
                curtain airbags and three-point seatbelts in all five seating 
                positions (those in the front have dual-stage pretensioners). 
                The front passenger airbag can also be deactivated to allow a 
                rear facing child seat to be fitted to the front seat and for 
                the first time Jazz is equipped with seatbelt reminders for both 
                the front and rear seats. Front seats are also fitted with 
                active headrests to minimise the potential for whiplash 
                injuries. </font></p>
