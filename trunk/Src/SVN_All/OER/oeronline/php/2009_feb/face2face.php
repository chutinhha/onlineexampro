            <td width="70%" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><br>
                <font face="Verdana" size="4"><b>Getting the fundamentals right</b><br>
            </font><font face="Verdana" size="1"><strong>Youssef Nasr, CEO, HSBC 
            Bank Middle East was in Muscat recently to participate in HSBC’s 60 
            years in Oman celebration. He takes time out from his busy schedule 
            to speak to Mayank Singh</strong><strong style="font-weight: 400"><br>
            <br>
            <img border="0" src="face1.jpg" align="left" hspace="8" vspace="6" width="300" height="296"></strong><strong>What 
            has enabled HSBC to do well in the Sultanate’s market?</strong><strong style="font-weight: 400"><br>
            The longevity in the market gives us credibility. We have been 
            fortunate in attracting some of the best graduates in the market and 
            we are impressed by the positions that these people have reached in 
            the commercial, business and government sectors in the country. Oman 
            has been an open economy for a long time. The other day I was at the 
            Salalah museum and I saw the painting of an Omani ambassador who 
            showed up in China 1000 years ago in the tenth century. So obviously 
            it is a country which is open to international trade and investment 
            and HSBC which has this connectivity does well in Oman. <br>
            <br>
            </strong><strong>How important is Oman for HSBC?</strong><strong style="font-weight: 400"><br>
            It is important for two reasons – one, the business here is good, 
            produces good returns and has a top notch client base. Second, we 
            are a global bank and when we speak to our global clients or 
            multinational clients, then it is important that we should be in a 
            position to serve them in as many countries as possible, a huge 
            network gives us credibility. <br>
            <br>
            </strong><strong>Creating jobs for nationals has been a stated 
            government objective. How has HSBC been doing on this count?</strong><strong style="font-weight: 400"><br>
            We are doing well and our Omanisation ratio has been hovering in the 
            low nineties, however there is always room to do better. We have 
            identified some Omani nationals who will now be receiving training 
            or get assignments elsewhere within the HSBC group. I see some 
            possibilities of having certain shared services in our Middle East 
            operations, some of which can get supported from Oman. I do see 
            opportunities for Omanis in cross postings in other parts of the 
            world too. Apart from recruiting high quality people, it is equally 
            important that we have a learning and development programme for our 
            existing employees. This helps in updating their skills, widening 
            their knowledge of the bank, and becoming more relevant to our 
            customers.
            </strong></font></p>
            <p align="center" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">
            <strong style="font-weight: 400">
            <img border="0" src="face.jpg" width="366" height="255"></strong></font></p>
            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1"><strong>
            Has the global financial meltdown led to any rethinking about the 
            bank’s strategy?
            </strong><strong style="font-weight: 400"><br>
            There is no rethink about our fundamental strategy, but yes we are 
            making some tactical adjustments. The example that I give is that 
            everyday you set out on the road that takes you to your office, you 
            know the road well, and you know where to get onto the highway where 
            to get off. If the highway gets hit by a snowstorm, this does not 
            mean that you would not know where to go, but you will be more 
            cautious, drive slowly and be more alert. And this is precisely what 
            we are doing as an organisation. The strategy, fundamentals, values 
            and culture of the bank remains the same, but yes if we feel that 
            there are certain risk exposures that have gone up, we pump the 
            brakes to slow such exposures. The growth in employment costs will 
            be monitored carefully. We are not talking about a reduction in 
            employee strength, but about not growing our employee base as 
            quickly as before. The fact that revenues and volumes will not grow 
            at the same pace as before, automatically brings down the need for 
            additional staff. These are the mid-course adjustments that any 
            organisation needs to make during a downturn. <br>
            <br>
            Is the bank going to be prudent to the extent of turning down 
            customer request for loans? <br>
            Banks do not make money and executives do not get rewarded for not 
            disbursing loans. You make money by giving loans, making profits and 
            that in turn grows the business. So there is no question of stopping 
            loans. But yes, if we feel that certain segments are prone to more 
            financial stress then we will tighten up the criteria for such 
            lending, but we are still in the business of lending money. As a 
            bank we are not just responsible for extending credit to people but 
            also responsible for protecting people’s savings. When we hold back 
            on lending, it is because we want to protect people who have 
            entrusted us with their money, we think that going beyond a certain 
            risk point would be prejudicing the safety and soundness of 
            depositors. It is just a question of finding the right balance 
            between the two sets of very important constituents that we have -- 
            borrowing clients and deposit clients. <br>
            <br>
            <img border="0" src="face2.jpg" align="right" hspace="8" vspace="6" width="174" height="546"></strong><strong>If 
            you were to do some crystal gazing, how long do you expect this 
            crisis to last?
            </strong><strong style="font-weight: 400"><br>
            I am optimistic because there is a tremendous amount of effort going 
            on globally to limit the magnitude of the decline and to put the 
            world economy on some growth, if not exuberant growth in 2009. The 
            governments have resorted to fiscal policies, like deficit spending, 
            stimulus packages etc, central banks are taking monetary policies, 
            like a big reduction in interest rates, expansion of their balance 
            sheets (to create liquidity) and the message that we are getting is 
            that they have not finished with what they are doing. They have put 
            through a series of packages and are seeing what is the likely 
            impact and there is a willingness to do more. I am not 
            underestimating the magnitude of the crisis, but I am recognising 
            the enormous effort that politicians and central bankers are putting 
            in to mitigate this crisis. <br>
            <br>
            </strong><strong>While Fed rates (or central bank rates) have come 
            down to zero, the rate of corporate loans is still high. How does 
            one break this vicious cycle?</strong><strong style="font-weight: 400"><br>
            The difference between the interest spreads (Fed rate and corporate 
            rates) is gradually coming down. Banks need to be reassured that 
            there is enough liquidity in the system. With Central Bank injecting 
            cash into the system, we are seeing a more relaxed atmosphere in 
            general liquidity. The second thing is that banks at some stage had 
            become scared of lending to each other, because of the well 
            publicised bankruptcies and government bailouts, we are seeing an 
            improvement in that. One well known indice for this is the OIS 
            index, which is the difference between the government rate and the 
            interbank rate at maturity. The government rate is the risk free 
            rate and the percentage that the interbank rate is above it shows 
            the degree of risk for banks. OIS spreads which had reached very 
            high levels, have gradually started coming down and though it is 
            still higher than the historical norm because of residual concerns 
            in the system, it is not way out of line. This shows that banks have 
            started lending to each other. The third phase would entail making 
            this credit available to companies and consumers. And this is where 
            government programmes and some of the commitments that the banks 
            have made might begin to yield results. For example, HSBC has 
            announced a $5bn worldwide programme for SME lending. In the UK we 
            announced an additional two billion pounds of mortgages in 2009, as 
            the UK government is concerned that a drying up of housing mortgage 
            would exacerbate the housing crisis.<br>
            <br>
            </strong><strong>Do you expect the world economy to recover soon is 
            there still a lot of pain to go through?</strong><strong style="font-weight: 400">
            <br>
            I think that it is just a matter of time before things improve, 
            because there have been other things that are aiding the recovery 
            process like greater consumer solvency. The decline in oil prices, 
            though this may not be ideal for OPEC countries, means that 
            consumers all around the world are spending less on transportation, 
            heating etc. Food prices have come down, a year back there were 
            riots in the streets for food. As prices have moderated there is 
            more disposable income left with a family. Commodity prices which 
            had gone through the roof, making new construction prohibitive has 
            come down. <br>
            <br>
            </strong><strong>Will different regions (or countries) come out of 
            the crisis with varying degrees of impact?</strong><strong style="font-weight: 400"><br>
            I believe so. So far we have had three countries in the GCC region, 
            which have announced their budgets for 2009, Oman, UAE and Saudi 
            Arabia, and all of them are spending more than what they spent last 
            year, either because they still have big revenues, even at current 
            oil prices or because they are ready to engage in some deficit or 
            dive into their accumulated reserves. I am also encouraged by the 
            fact that long term projects like infrastructure projects, 
            desalination plants, oil and gas projects and power generation plans 
            are still proceeding. So the turndown is not going to be as severe 
            in the GCC as in the West and so the recovery of the region will 
            also be faster. I expect that in the second half of 2009, world GDP 
            will be higher than the first half. A tipping point will occur 
            between the first half and second half of 2009. Some countries have 
            more pain to go through than others. US will come out faster than 
            Europe, China will come out fast. In the region Oman, Saudi and 
            Qatar will be stronger than the others. <br>
            <br>
            </strong><strong>Looking back, what would you think went wrong with 
            the system?</strong><strong style="font-weight: 400"><br>
            It is a series of things, whenever you get a crisis of this 
            magnitude you cannot look for a single factor. We had a period of 
            very easy money, which did not help. We had a period when 
            traditional controls were put aside. An excess of greed and wanting 
            to make an extra buck led us to lose sight of human values like 
            working hard for a period of time to earn. This gave way to instant 
            gratification. We had a political climate, where government 
            intervention was frowned upon. It was presumed that there will be 
            self regulation as opposed to government regulation that did not 
            happen to the extent that was expected. There were imbalances which 
            were allowed to go on. One such imbalance was the low savings rate 
            in the US. People were saving much less because the rest of the 
            world was too happy to send capital to the US.<br>
            <br>
            </strong><strong>Is this the end of free market capitalism and the 
            return of big government?</strong><strong style="font-weight: 400"><br>
            I do not believe that it is the end of free market capitalism, 
            because there is no economic system that is better. We will have 
            bigger governments, through the banks that have been nationalised 
            and the support mechanism that they have put in. Governments will 
            also get bigger because they will beef up their regulatory agencies 
            to prevent such excesses. So we will have bigger governments but 
            this will not lead to a cancellation of the free market economy.
            </strong></font></p>
