            PERFECTION </font></b><font face="Verdana" size="1"><br>
            <b>Ketan Mehta remains a student of fine arts years after his formal 
            education and it remains a recurring theme of his life. By Mayank 
            Singh<br>
            </b><br>
            <img border="0" src="beyond.jpg" align="left" hspace="8" vspace="6" width="250" height="302">It 
            is difficult to miss the passion with which Ketan Mehta, assistant 
            general manager, advertising &amp; PR, Suhail Bahwan Group speaks about 
            painting, arts and design. “I am completely mesmerised by Lenonardo 
            da Vinci’s ability to invent propellers and to paint the Monalisa, 
            artists may come and go but his art cannot be surpassed.” On M F 
            Husain, the Indian painter, who has been called the Picasso of India 
            by Forbes magazine he says, “The greatness of Husain is the freedom 
            with which he approaches his subject and the way he expresses 
            himself.”<br>
            <br>
            Having grown up in a family with a strong association with arts and 
            design, Mehta developed a love for fine arts early in life. His 
            father, Rajendra Mehta studied art and design at the J J School of 
            Arts, Mumbai while his mother was proficient in handicrafts. Given 
            such an upbringing, it is little wonder that he went onto to do a 
            graduation from the Faculty of Fine Arts, Maharaja Sayajirao 
            University, Vadodara. “I was preparing for an architecture course, 
            when the faculty of Fine Arts announced an aptitude test, I reached 
            there unprepared and surprisingly stood third.” He went onto to 
            specialise in advertising and graphic design. Architecture’s loss 
            proved to be advertising’s gain. Mehta is now a devout MacAddict.<br>
            <br>
            <b>Sports buff</b><br>
            <img border="0" src="beyond1.jpg" align="right" hspace="8" vspace="6" width="175" height="150">Like his career, Golf too happened by chance. The Suhail Bahwan 
            Group companies sponsor various golf championships. Mehta used to be 
            entrusted with the responsibility of carrying out all the 
            arrangements every year. “A gentleman walked to me one day and said 
            ‘I see you here regularly, do you play Golf’,” recalls Mehta. That 
            simple query gave him the idea of giving the game a shot. What 
            started as a casual sport in 2004, soon gave way to a fierce 
            competitive spirit as Mehta started doing the rounds of the golf 
            course regularly. “We used to reach Al Maha Golf Club at 4.30am 
            waiting for the first sunray to take a shot in the summer.” He plays 
            close to 5 hours of golf on weekends now. To avoid ruffling feathers 
            at home, he is at the course early and finishes early on Fridays. An 
            avid sportsman, Mehta represented his faculty in cricket at the 
            University level. He has been an avid tennis player for the last 20 
            years. Coming back to golf he says, “It is the only game where 
            nothing except you and nature matters, a game in which you play 
            against yourself.” Whether it is the frustration of a shot gone 
            wrong or the ecstasy of having pared a hole, the ups and downs of 
            the game is a pull that few golfers can resist.<br>
            <br>
            <b>Culture streak</b><br>
            His love for design and the arts colours his choice of travel 
            destinations. Mehta is partial to destinations with a rich cultural 
            heritage. “I can spend days in Rome, the experience of walking on a 
            staircase designed by Michelangelo or visiting the Sistine Chapel at 
            the Vatican is breathtaking. Just a thought that the time 
            Michelangelo was painting the ceiling, Raphael was painting his 
            fresco, ‘School of Athens’ itself is invigorating,” he says. Paris 
            runs as a close second to Rome due to Louvre. An avid photographer, 
            Mehta has been shooting images for Suhail Bahwan Group calendars for 
            quite a few years. Looking back he feels that the freedom given to 
            him by his father has played an important role in his success. </font></p>
