                </b><b>
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">THE CEO 
            CONCLAVE – MARINA 2009</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>The conclave brought leaders of the industry to discuss, plan and 
            execute performance and people management challenges<br>
            </b><br>
            <img border="0" src="news.jpg" align="left" hspace="8" vspace="6" width="250" height="163">For 
            an organisation to be among the best in its industry and to perform 
            above customer and shareholder expectations, the need for an 
            effective Performance Management System is imperative. It is even 
            more important to have an objective system that articulates 
            individual performance to organisational goals.<br>
            <br>
            To highlight the need of such systems and the process of 
            implementing them, Infoline and the Performance Factory brought 
            together business leaders of the country for a half day session 
            filled with informative presentations and a performance based 
            activity aboard a catamaran. Titled the CEO Conclave – Marina 2009, 
            the forum brought leaders of the industry to discuss, plan and 
            execute performance and people management challenges. The forum also 
            provided viable solutions for corporate Oman to stay ahead of the 
            times and be geared for various business situations.<br>
            <br>
            The event was held at the scenic environs of the Marina Bandar where 
            the Blue Marlin restaurant provided the backdrop for the first 
            session followed by an exciting activity and net-working cruise 
            aboard the SY Azzura.</font></p>

            <p align="center" dir="ltr" style="margin-left: 5; margin-right: 5"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="news2.jpg" width="370" height="304"></font></p>

            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">The day began with the launch of the CEO Conclave logo and a brief audio visual introduction to Infoline 
            and the Performance Factory. Sameer Gupta, Executive Vice-President 
            of Infoline then welcomed the gathering followed by the launch and 
            an insightful presentation on HR BPO services. Infoline’s 
            technological expertise and Performance Factory’s knowledge 
            expertise will be combined to provide an extensive range of HR BPO 
            solutions which will be devised, designed, and customised to handle 
            every administrative, managerial, consultation, and support function 
            concerned with the Human Resources function and will ensure 
            optimised cost control. These services will ensure organisations 
            transform the way they do business and help them optimise 
            performance by focusing on core competencies. The presentation wound 
            up with an invigorating and enthusiastic open discussion on top 
            talent challenges in Oman.<br>
            <br>
            <b>Performance drive<br>
            </b>
            <img border="0" src="news1.jpg" align="right" hspace="8" vspace="6" width="250" height="165">In the open discussion most of the CEOs highlighted the need for 
            a performance driven culture in organisations as the most important 
            area of focus. A performance driven culture is a reflection of a 
            system which ensures that an organisation continually sets and 
            measures goals, creates an environment that is congenial for talent 
            development and personal growth of employees while ensuring that the 
            needs of its customers are met at all times. Critical to building a 
            culture of personal growth and engaging talented people is a 
            disciplined performance management system. Organisations that thrive 
            to improve and change in the face of stiff competition in today’s 
            markets have to ensure that they measure performance at an 
            organisational level while ensuring that their employees are 
            continually assessed and coached on enhancing their competencies.<br>
            <br>
            The 
            CEOs also stressed on the gaps in leadership pipeline and skill gaps 
            in critical positions as the next top challenges and called for the 
            right training to up skill the workforce. Organisations worldwide 
            are facing acute shortage of skilled and experienced leaders. While 
            the industrial revolution focused on the technical aspects of talent 
            development, the information age has been one of rapid business 
            expansion, technological developments and changing market dimensions 
            allowing no time for employees to gain critical experience in 
            leadership and management. Although we could argue that the new 
            generation has a lot more exposure to better education and training, 
            the fact remains that there has been little scope for application of 
            that knowledge due to unprecedented growth.<br>
            <br>
            The CEOs were then taken aboard the catamaran SY Azzura for a 
            45-minute cruise during which the business heads participated and 
            completely enjoyed a performance based activity which required teams 
            to manually send one of their colleagues up the mast of the 
            catamaran. The time taken to reach a pre-determined target and the 
            amount of resources used to achieve the task were marked as critical 
            indicators to showcase performance. <br>
            <br>
            Both Infoline and the Performance Factory plan to follow the CEO 
            Conclave with a series of workshops and seminars on performance and 
            people management issues and the next Conclave in 2010 plans to 
            highlight another facet of these challenges. The event was sponsored 
            by Oman Economic Review and Amouage and their patronage helped bring 
            the Conclave to a grand success. </font></p>

