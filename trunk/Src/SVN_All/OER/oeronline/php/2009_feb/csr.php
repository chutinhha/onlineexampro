				<b><font face="Verdana" size="4">Service above self<br>
                </font></b><font face="Verdana" size="1"><b>Oman will join the 
                rank of countries having a Rotary Club this year as a committed 
                group of individuals are working on the effort earnestly. By 
                Mayank Singh <br>
                <br>
                <img border="0" src="csr.jpg" align="left" hspace="8" vspace="6" width="366" height="221"></b>The 
                Sultanate may get its first Rotary Club soon. The initiative has 
                been in the works for a while, as the first efforts to get the 
                Club going dates back to 2004 when a delegation from the Rotary 
                Club of Brussel-Coudenberg visited the sultanate and discussed 
                the idea at a ministerial level. Though the idea found all round 
                acceptance, Rotary failed to take off. In 2008 a group of 
                ex-Rotarians revitalised the efforts. Says Henk Jaap Kloosterman, 
                deputy study centre manager, PDO and a Rotarian, “We have the 
                critical mass now as 60 people have shown interest, out of which 
                25 are quite active.”<br>
                <br>
                From a legal point of view such an association needs the support 
                of at least 40 founder members. The group is working on filing 
                an application with the Ministry of Social Affairs to get the 
                requisite approvals. The Rotary Club of Muscat is likely to be 
                the first club in Oman to be chartered as district 2450.<br>
                <br>
                <b>Tracing the roots<br>
                </b>Rotary International, the world’s first service club 
                organisation is made up of nearly 33,000 clubs in more than 200 
                countries and geographical areas. Its members form a global 
                network of business and professional leaders who volunteer their 
                time and talents to serve their communities and the world. 
                Chicago attorney Paul Harris, started the Rotary Club of Chicago 
                in 1905 and since then the organisation has spread worldwide.<br>
                <br>
                Rotary’s motto, Service Above Self exemplifies the humanitarian 
                spirit of the organisation’s 1.2mn members. The Club is 
                characterised by a strong sense of fellowship, meaningful 
                community and international service projects. The Rotary 
                Foundation is a not-for-profit corporation whose mission is to 
                enable Rotarians to advance world understanding, goodwill and 
                peace through the improvement of health, the support of 
                education and the alleviation of poverty.<br>
                <br>
                <img border="0" src="csr1.jpg" align="right" hspace="8" vspace="6" width="170" height="152">Eradicating 
                polio has been a priority for Rotary International. It has 
                raised more than $650mn in support for the PolioPlus programme 
                that provides funding for mass immunisation campaigns as well as 
                support for social mobilisation, surveillance and laboratories 
                to help carry out the final stages of global polio eradication. 
                In addition it has provided hundreds of thousands of volunteer 
                hours. Rotary has partnered with the World Health Organiasation, 
                UNICEF and US Centres for Disease Control and Prevention in the 
                initiative to eradicate polio worldwide. As a result of these 
                efforts, two billion children under the age five have received 
                the polio vaccine, five million people who might otherwise have 
                been paralysed are walking today, 500,000 new cases of polio are 
                prevented each year and the number of polio cases have declined 
                by 99 per cent worldwide. Apart from eradicating polio, Rotary 
                International addresses other critical issues of health and 
                hunger, water management and literacy through projects both at 
                the local and global level.<br>
                <br>
                Says Kloosterman, “We have made a conscious effort to select 
                leaders from different walks of life.” Now that a start has been 
                made, one can look forward to Rotary taking off in a big way 
                soon in the Sultanate.
