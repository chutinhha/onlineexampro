				<p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4">
				<br>
				INTERIORS &amp; BUILDEX 2009:<br>
				</font><font face="Verdana" size="4">Serving the building and 
                construction industry in Oman</font></b><font face="Verdana" size="1"><br>
                <br>
                <img border="0" src="event.jpg" align="left" hspace="8" vspace="6" width="350" height="235">Interiors 
                &amp; Buildex 2009, the biggest exhibition for the building and 
                construction industry in Oman, is all set to showcase a wide 
                range of products and equipment, with the participation of a 
                record number of exhibitors from within the sultanate and from 
                other countries worldwide.<br>
                <br>
                Scheduled on March 16-18, 2009 at the Oman International 
                Exhibition Centre in Muscat, Interiors &amp; Buildex 2009 will 
                feature all requirements of Oman’s building &amp; construction 
                industry, which is now experiencing rapid growth as a result of 
                the implementation of various multimillion-dollar projects in 
                many parts of the country.<br>
                <br>
                “The construction of prestigious residential properties, major 
                industrial projects and world-class tourism infrastructure has 
                greatly boosted the demand in Oman for all types of building 
                materials, interior furnishings, heavy equipment and other 
                construction supplies,” said C.J. Paul, general manager of 
                Omanexpo LLC, the organiser of the event and the leading 
                exhibition management company in the sultanate.<br>
                <br>
                Paul pointed out that Interiors &amp; Buildex aims to serve the 
                growing requirements of the building and construction industry 
                in Oman as the country pushes ahead with its range of 
                development plans. In line with this, now under construction are 
                various major industrial, commercial and residential projects, 
                all of which are consistent with the government’s strategy to 
                further diversify its economy.<br>
                <br>
                These major projects have turned Oman into a highly lucrative 
                market for everything required in any type of construction 
                activity or interior design project. A wide range of these 
                requirements will be prominently displayed at Interiors &amp; 
                Buildex 2009, which has developed a reputation over the years as 
                the ideal venue for promoting products and equipment for Oman’s 
                building and construction industry. <br>
                <br>
                Among the wide selection of products that will be displayed 
                include air-conditioning systems, cement and concrete products, 
                bathroom fittings, ceramic wall and floor tiles, paint products, 
                heavy equipment, kitchen modules and appliances, doors and 
                windows, construction vehicles, marble products, interior 
                furnishings, and many other products.<br>
                <br>
                The exhibition will feature the stands of all leading companies 
                in Oman’s building and construction industry. In addition, 
                Interiors &amp; Buildex 2009 will also feature the national 
                pavilions of Turkey, Kuwait and Thailand as well as exhibitors 
                from India, China, Egypt, the United Arab Emirates, and many 
                other countries. Interiors &amp; Buildex 2009 is supported by 
                Windows 2000 as the platinum sponsor, Panorama and Dosteen Doors 
                &amp; Engineering Services LLC as the gold sponsors, and IQue as the 
                silver sponsor. The event is also officially endorsed by Oman’s 
                Public Establishment for Industrial Estates (PEIE), the Oman 
                Society of Engineers, UK Trade &amp; Investment, and the German 
                Industry and Commerce – Oman, Qatar and UAE, and the Embassy of 
                the Republic of Turkey in Muscat. <br>
                <br>
                The international exhibition also has the backing of various 
                industry publications, including Construction Week as the 
                official publication, Arab Construction World as the official 
                magazine, OER Dossier-Construction and Al-Maskan Magazine as 
                media partners, Architecture Plus as the official architecture 
                and design partner, as well as Construction World and CW 
                Interiors as the international official magazines.<br>
                <br>
