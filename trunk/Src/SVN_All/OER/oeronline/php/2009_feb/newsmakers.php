			</font></b><b><font face="Verdana" size="4">A DREAM COME TRUE<br>
            </font></b><font face="Verdana" size="1"><b>Oman’s football team has 
            given the country it’s best gift – the 19th Arabian Gulf Cup<br>
            </b><br>
            <img border="0" src="newsakers.jpg" align="left" hspace="8" vspace="6" width="350" height="178">A 
            nation held its breath as the finals of the 19th Arabian Gulf Cup 
            went into penalty shootout’s. And as Oman scored the winning goal, 
            the country exploded in rapturous celebrations. And going by the 
            looks of it, the celebrations will not end soon. It was third time 
            lucky for the Sultanate as they lifted the coveted Gulf Cup by 
            defeating Saudi Arabia on January 17. For nearly two weeks, the 
            whole of Oman was gripped by soccer fever, and when it mattered, the 
            national team delivered, lifting the glittering cup. <br>
            <br>
            The Omani team was on a roll , beating one formidable opponent after 
            another en route to their third consecutive final, where they came 
            up against a strong and fighting Saudi Arabia. The valiant team 
            worsted their fancied rivals 6-5 in sudden death penalty shootout. 
            The two finalists, undoubtedly the two best teams of the tournament, 
            matching each other in every department, fought tooth and nail but 
            nothing separated them after 120 minutes of fierce battle. In the 
            ensuing penalty shootout too, the teams were tied 5-5 with Yasser al 
            Qahtani, Saud Kariri, Redha Thakar, Ahmed Utaif and Usama Hawsawi 
            succeeding for the Saudis while Khalifa Ayil, Ismail al Ajmi, Hassan 
            Mudhaffar, Hashim Saleh and Fauzi Basheer were on target for Oman.<br>
            <br>
            It was in the sudden-death that dame luck favoured Oman. Saudi 
            Arabia’s Tayseer Jassim, probably overawed by the occasion and with 
            a dominating Ali al Habsi in front of him, shot the ball wide. And 
            then, captain Mohammed Rabee’a, the man who led the Sultanate to 
            finals of the 17th and 18th Gulf Cups in Doha and Abu Dhabi 
            respectively, walked in to take the kick. And he deservedly put the 
            ball behind the Saudi custodian Waleed Abdullah sparking 
            celebrations at the stadium and all over the Sultanate. While the 
            Cup triumph brought joy to the entire nation, best team for fair 
            play and two individual awards came as an icing on the cake for the 
            Omanis.<br>
            <br>
            In fact, the player of the final too was clinched by an Omani when 
            Ahmed Mubarak ‘Kanu’ had to be literally pulled out from his 
            celebrating team-mates before handing the trophy. Mohammed Rabee’a 
            walked up the pedestal to first take the fair play trophy from chief 
            guest His Highness Sayyid Haitham bin Tareq al Said. It was then the 
            turn of peerless Ali al Habsi to lift his best goalkeeper award for 
            fourth time in a row. Young Hassan Rabee’a, truly the find of the 
            tournament, lifted the top scorer award after scoring four goals 
            three of which came in one match against Iraq. The best player of 
            the championship award, however, went to Saudi Arabia’s Majid al 
            Marshadi.<br>
            <br>
            And finally Mohammed Rabee’a walked up to receive the winners’ 
            trophy and as soon as the Omani captain got his hands on the coveted 
            Cup, his team-mates grabbed it running on to the field in 
            celebration and to do a lap of honour in front of thousands of their 
            fans. “It is a great moment for all the Omanis. The team did the 
            nation proud,” said Oman’s coach Claude le Roy. “It is fantastic 
            feeling to win the Gulf Cup. It was a very high level performance by 
            the Omanis. The quality of the game was good. We worked hard on our 
            tactics and it paid off. All credit to the players who were very 
            dedicated and determined.” </font></p>
