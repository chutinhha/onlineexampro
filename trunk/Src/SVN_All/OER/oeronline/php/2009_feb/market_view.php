                <font face="Verdana" size="1"><br>
                In another major event, National Gas announced the formation of 
                a new 50/50 Joint Venture “Unigaz LLC” with paid up capital of 
                RO150,000, in partnership with Unigaz International WLL, 
                Bahrain. Oman Flour Mills (OFM) is forming an LLC investment 
                company with a capital of RO150,000 to manage its existing 
                investments and forthcoming projects. The new LLC company will 
                be a subsidiary of OFM.<br>
                <br>
                In regulatory changes, liquidity elevating measures were put 
                forward by the Central Bank of Oman reducing the bank reserve 
                requirements ratio to three per cent from five per cent and 
                raising the lending ratio to 85 per cent from 82.5 per cent.
