                <b><br>
                GCC markets<br>
                </b>Markets across the GCC suffered the brunt of falling crude 
                oil prices. Uncertainty on oil prices has been the real cause of 
                the fall in these markets. The markets throughout GCC 
                experienced lower volumes and investors seem to be more 
                comfortable holding on to their cash. <br>
                <br>
                Kuwait fell the most for the period from 16 December 2008 to 15 
                January 2009 due to credit crisis worries. Top Kuwaiti bank 
                Global Investment House defaulted on its $3bn debt obligations, 
                raising concerns that other Arab Gulf financial firms may follow 
                as the world financial crisis is spreading to the region. Saudi 
                markets bucked the trend and closed up for the same period on 
