                    </font><b><font face="Verdana" size="4">Little to cheer
                </font></b><font face="Verdana" size="1"><br>
                <b>The new year began on a muted note for most regional bourses 
                as investors remained apprehensive of what lies ahead<br>
                </b><br>
                MSM30 continued to slide down in 2009, as global recession 
                concerns persist. The benchmark MSM30 index, treaded down 16 per 
                cent for the period from 16 December 2008 to 15 January 2009 led 
                mainly by banking and industry stocks. Expectation of higher 
                provisioning and impairments led to a steep fall in banking 
                stocks, while demand contraction worries led to a fall in 
                industrial companies.<br>
                <br>
                Average turnover per day decreased to RO3mn for the period from 
                16 December 2008 to 15 January 2009 compared to RO4mn in the 
                previous period. Lower volumes can be attributed to holiday 
                season and risk aversion by investors. <br>
                <br>
                Oman’s budget 2009 is development oriented, however on a 
                slightly higher average oil price assumption ($45 per barrel). 
                Oil price traded below $40 per barrel, raising fears on budget 
                spending. Despite the fact that, Oman’s government has avenues 
                to finance the revenue and expenditure gap, the continuing 
                sluggish demand for oil increases worries about a cut in 
                spending.<br>
                <br>
                <b>Corporate happenings<br>
                </b>In key corporate events, Shell Oman won contracts in 
                aviation fuel segment from PDO (term 3 years) and Qatar Airways 
                (term one year). Al Hassan Engineering was awarded an EPC 
                Contract of Burhan Flowline Project, which include procurement 
                and construction by PDO for an amount of RO11mn. Renaissance 
                Services subsidiary Topaz Energy and Marine Limited won a 
                contract with oil major Total E&amp;P-Qatar, for two of its vessels 
                for a period of two years.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
