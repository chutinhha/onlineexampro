                  Supermarkets join forces to reduce plastic bag usage</font></font></b><font face="Verdana" size="1"><br>
                  Oman Environment Day, which was held recently, saw fourteen 
                  supermarkets join forces with the aim of reducing plastic bag 
                  usage in the Sultanate, as part of the ongoing efforts of the 
                  Environment Society of Oman’s (ESO) “Say No to Plastic Bags 
                  Campaign”. The supermarket campaign was the latest initiative 
                  by the ESO, with the support of Oman LNG, to educate consumers 
                  on the effects of plastic bags on the environment. The 
                  Environment Day provided the platform to persuade Oman’s 
                  consumers to either reuse their plastic bags or opt for 
                  reusable jute bags. Furthermore, the free distribution of jute 
                  bags in each of the fourteen outlets on the day, meant all 
                  customers were given the opportunity to ‘go green’ at no 
                  additional cost.<br>
