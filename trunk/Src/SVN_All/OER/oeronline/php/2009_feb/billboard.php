                  NBO announces 2008 Full Year Results</font></font></b><font face="Verdana" size="1"><br>
                  National Bank of Oman has announced its results for the year 
                  ended 2008. “Net profit for the year of RO45.4mn, reflects 
                  sustained evidence of the Bank’s successful growth strategy. 
                  The operating profit for the year 2008 increased by 49 per 
                  cent to RO54.3mn compared with RO36.4mn for the prior year,” a 
                  release issued by the Bank states. The Board of Directors has 
                  proposed a cash dividend of 17.5 per cent of capital to 
                  shareholders subject to receipt of regulatory and 
                  Shareholders’ approval.<br>
                  <br>
                  Sheikh Suhail Salim Bahwan, the Chairman of the Board of 
                  Directors said: “Our performance continued to be inspired by 
                  Oman’s vision and its leadership. Despite a very turbulent 
                  fourth quarter, and after an impairment charge of RO3.7mn on 
                  available-for-sale investment in the income statement, the 
                  Bank reported satisfactory results for the full year with a 
                  Net Profit of RO10.5mn for the fourth quarter.”<br>
                  <br>
                  “Our strategy, that focused on business growth and alignment 
                  of business activities with our strategic partner, Commercial 
                  Bank of Qatar, has resulted in a robust earnings performance 
                  from all of our core businesses. We would also like to 
                  acknowledge the trust and confidence of our customers in the 
                  services that the Bank provides,” he said. </font></p>
