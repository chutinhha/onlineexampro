                  Late Omani poet’s book available now</font></font></b><font face="Verdana" size="1"><br>
                  A new book of poems ‘In the Dimmest of Light’ written by the 
                  young Omani poet, Ali Mehdi, who passed away in June 2008, is 
                  now available in Muscat’s bookshops. The book’s publication 
                  fulfills late Ali’s wish of having his third book of poetry 
                  published by the end of the year. Like his previous two works, 
                  it consists of sections, each one an exploration of emotions 
                  and ideas every living being can identify with. In this 
                  compilation of recent and earlier unpublished work, the poet 
                  speaks from his heart to express the universal language of 
                  desire and joy, sadness and heartache.<br>
                  <br>
                  Profits from the sales of ‘In the Dimmest of Light’ will go to 
                  a charitable society and Ali’s family are working towards 
                  setting up a small fund to support an electronic (Web based) 
                  literary competition for young poets and writers.<br>
