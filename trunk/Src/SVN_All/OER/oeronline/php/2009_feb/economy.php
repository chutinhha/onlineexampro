		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font>
				<b><font face="Verdana" size="4">BREAKING TRADE BARRIERS<br>
                </font><font face="Verdana" size="1">Oman is faring remarkably 
                well compared to some of its regional counterparts and is making 
                good on its export-based diversification plan. The enactment of 
                the US Oman FTA illustrates the stability of the country’s 
                economy as well as its ever-growing trade ties<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px">
				<b>
				<font face="Verdana" size="1" color="#CC3333">By Oliver Cornock</font></b><font face="Verdana" size="1"><br>
                <br>
                <img border="0" src="economy.jpg" align="right" hspace="8" vspace="6" width="125" height="148">Oman kicked off the New Year 
                with a reassuring nod to free trade during a time of growing 
                fears of protectionism on a global scale. January 1, 2009 saw 
                the entry into force of the US-Oman free trade agreement (FTA), 
                building on the Gulf Cooperation Council’s December announcement 
                of a collective FTA with Singapore. As the domino-effects of the 
                US sub-prime housing crisis roll into 2009, a debate is emerging 
                about which countries will be tempted towards an isolationist 
                trade stance, thereby exacerbating the current crisis; and which 
                will fully engage in international free market principles. Oman 
                has already made it clear that not only is it firmly on the side 
                of the latter, but it is also faring remarkably well in these 
                days of economic doom and gloom.<br>
                <br>
                As liquidity dries up, job markets shrink, and growth outlooks 
                are being slashed across the globe, some world leaders are 
                facing the temptation to throw up the ramparts in the interest 
                of protecting domestic industries and jobs. A reaction such as 
                this to the economic crisis is, as a recent article in The 
                Economist declared, “individually appealing, (but) collectively 
                futile.”<br>
                <br>
                <b>Protectionist mores<br>
                </b>The world has already seen such a scenario play out – during 
                the 1930s, in the aftermath of the 1929 American stock market 
                crash – with dire results. From January 1929 (pre-crash) to 
                January 1933, world trade shrunk from $5.3bn to $1.8bn. This 
                represents a decline of 66 per cent of trading volume and was 
                largely attributable to a dramatic increase in tariffs and a 
                widespread adoption of protectionism.<br>
                <br>
                Indeed, if prominent trading countries were to take measures 
                similar to those of the Depression-era the collective recovery 
                of the world economy would be paralysed and the return to 
                pre-crisis growth levels would be markedly delayed.<br>
                <br>
                The approach of Oman and the wider GCC offer a modicum of 
                comfort – something increasingly hard to come by lately – to 
                foreign investors fearful of a further slowdown. Between 
                December of 2008 and the first days of 2009, Oman, either 
                independently or within the GCC bloc, enacted two major FTAs in 
                a robust endorsement of the importance of open trade routes 
                toward the rehabilitation of the global economy.<br>
                <br>
                In December the GCC signed a FTA with Singapore, reinforcing a 
                bilateral link that has been steadily strengthening through not 
                only trade, but also technology exchange and banking. The GSFTA, 
                as the agreement is called, is the first signed by the GCC with 
                the region and should give a boost to the healthy flow of 
                investment, trade, and business opportunities. From 2002 to 2007 
                GCC-Singapore trade increased by 127 per cent. Prior to the 
                agreement, only 10 per cent of Singaporean goods were duty free 
                into the GCC; now virtually all (99 per cent) will flow into the 
                region without duty. In return, all goods of GCC-origin will 
                pass into Singapore untaxed.<br>
                <br>
                <img border="0" src="economy1.jpg" align="left" hspace="8" vspace="6" width="172" height="508">Moreover, the GSFTA is a sign that additional trade agreements 
                between Asian nations and the GCC, either collectively or with 
                individual states, are likely to come to fruition in the near 
                future. A FTA between the GCC and India is under negotiation, 
                for example, as trade ties continue to solidify. <br>
                <br>
                <b>Trade ties</b><br>
                Within this GCC push for a more formalised trade activity, Oman 
                is emerging as a leading example of a successful and diverse 
                trade strategy. The country has been steadily developing 
                extensive trade ties with India independently. In 2005 the value 
                of direct trade between the two countries totalled $480m, 
                building on demand from the large in-country Indian community. 
                By 2007 this number, bolstered by the 2006 start of the 
                Omani-Indian Fertiliser Company, reached $1.8bn and is expected 
                to breach the $2bn mark in 2008.<br>
                <br>
                In a recent interview with OBG, Prime Minister of India, 
                Manmohan Singh pointed to the Sultanate as an anchor for 
                relations between his country and the Gulf amidst the 
                complicated economic climate. He said “Against the background of 
                (the global downturn), I suggest there is an even greater need 
                for us to join hands to shape the counter-cyclical growth 
                strategies by focusing on the real economy. India and Oman are 
                well placed to convert this challenge into an opportunity.”<br>
                <br>
                Indeed, Oman is faring remarkably well compared to some of its 
                regional counterparts and is making good on its export-based 
                diversification plan. The enactment of the long-negotiated FTA 
                between the Sultanate and the United States that took place on 
                the first day of 2009, illustrated the stability of the 
                country’s economy as well as its ever-growing trade ties.<br>
                <br>
                The US FTA, which has been under negotiation since 2006, will 
                allow Oman to move into the $14trn US economy with few 
                impediments. Under the deal Oman will provide immediate 
                duty-free access to American goods, barring a selection of 
                vegetables and other products like tobacco that will be 
                gradually brought to duty-free status over a period of ten 
                years. In return the United States will eliminate duties on 
                virtually all Omani products with a few designated for a gradual 
                decrease to zero. The FTA builds upon the diplomatic history 
                between the two countries, dating from 1833, and will provide a 
                boost in Omani exports to the country, which totalled three per 
                cent and four per cent of total exports in 2005 and 2006, 
                respectively. The agreement makes the Sultanate one of only four 
                Arab countries (the others being Bahrain, Jordan and Morocco) to 
                conclude an FTA with the Western giant and implicitly states 
                that Oman’s labour practices and humanitarian commitments meet 
                the critical standards of the US.<br>
                <br>
                While the level of consumption in America has been hit hard by 
                the domestic credit crisis – the US trade deficit dropped nearly 
                29 per cent in November 2008 – it is only a matter of time until 
                it rebounds, bringing with it demand for exports of all kinds, 
                including Omani products. The relative strength of the greenback 
                at the start of 2009 is a sign that importing goods will become 
                less financially painful. <br>
                <br>
                In the meantime Oman stands to benefit from the FTA in the form 
                of job creation, increased investment and measurable steps 
                toward the Sultanate’s goal of economic liberalisation through 
                an export-based economy. As of mid-January yet another FTA, this 
                one between the GCC and the EU looked to be nearing conclusion 
                after over 10 years of tumultuous negotiations. If the deal is 
                finalised, Oman and the GCC stand to leverage their energy 
                capabilities to correct the trade imbalance that now stands in 
                the EU’s favour. <br>
                <br>
                At the start of 2009 jittery investors and world leaders around 
                the globe have reason to breathe at least slightly easier, as 
                the crop of FTAs centered around the GCC that have come up 
                recently indicate that the spectre of protectionism may at last 
                vanish. <br>
