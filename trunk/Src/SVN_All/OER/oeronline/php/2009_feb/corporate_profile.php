		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>Brimming with 
                activities <br>
                </b></font><font face="Verdana" size="1"><b>Oiltanking Odfjell 
                Terminals, which began its operations at the Sohar Port towards 
                the middle of 2008, has proved to be a major catalyst for 
                cost-effective operations of many companies based
                in Sohar, writes Visvas Paul D Karra<br>
                </b><br>
                <img border="0" src="corp.jpg" align="left" hspace="8" vspace="6" width="250" height="286">A chemical company based in Sohar 
                was having a huge logistics problem organising its feedstock 
                requirements. Every time a tanker arrived with feedstock, the 
                company had to mobilise a large number of trucks to transfer the 
                liquid from the port to their facility, virtually in a frenzy 
                because the more days the ship stayed docked at the port, the 
                higher the costs incurred by the company. But things took a 
                better shape in August 2008, when Oiltanking Odfjell Terminals &amp; 
                Co (OOTO) successfully started its bulk liquid storage 
                operations at its state-of-the art tank terminal located in the 
                Sohar Industrial Port.<br>
                <br>
                Now the chemical company has leased a tank at OOTO into which 
                the feedstock is pumped within one day. From here the product is 
                transported by truck to its production facility located in Sohar 
                Industrial Estate at leisure. It enables the manufacturer to 
                organise its feedstock requirements in such a way that the most 
                economical logistic solutions are achieved. Incidentally, the 
                first cargo into tank was received by OOTO in August 2008 
                specifically for this company.<br>
                <br>
                <b>State-of-the-art facility</b><br>
                According to Gerard Reumer, CEO of OOTO in Oman, the company has 
                invested around $240mn to build a state-of-the-art facility 
                which basically consists of two parts – a 36-hectare tank farm 
                for storage of bulk liquid and six liquid berths for ships. 
                Operations-wise, OOTO received its first petroleum cargo through 
                a pump over from the Sohar Refinery in August 2008 and in 
                October, the first cargo of petroleum was successfully loaded 
                ex-tank via OOTO infrastructure onto a vessel on one of OOTO’s 
                deepwater marine jetties. <br>
                <br>
                OOTO commissioned 537,000 cubic meters by the end of the year 
                and another 3045,500 cbm is expected to be put into operation in 
                the first and second quarter of 2009. Says Reumer, “The 
                terminal’s infrastructure provides excellent flexibility to its 
                customers, quick turnaround times for vessels and has added 
                capabilities such as truck loading, blending and injecting of 
                additives. OOTO’s tank terminal has multiple pipeline 
                connections to the Sohar Refinery owned by Oman Refineries and 
                Petrochemicals Companies.”<br>
                <br>
                Since successfully starting its bulk liquid storage operations 
                at its terminal located in the Sohar Industrial Port, OOTO has 
                put 537,000 cubic meters into operation to cater for the storage 
                and handling of clean petroleum products and chemicals. It is 
                the first phase of the 842,500 cbm facility which is due for 
                completion in the first half of 2009. Part of OOTO’s capacity 
                will be used by Aromatics Oman for the storage of feedstock and 
                produced product. OOTO will be actively involved in the 
                production process of this world-scale aromatics plant by 
                continuously pumping feedstock into the production process and 
                at the same time receiving produced product such as chemicals 
                and gases in tank before loading these onto vessels for export.<br>
                <br>
                <b>Higher ground</b><br>
                Says Reumer: “Besides the current scope, OOTO continues to 
                pursue various other projects with the aim to transform Sohar 
                into a major liquids hub in the region and in this way 
                contribute to Oman’s credentials as a petrochemicals giant. 
                Besides exploring other opportunities provided by international 
                trade OOTO aims to further integrate itself with production 
                facilities of any company in the industrial port who wish to 
                capitalise on the economies of scale the central tank farm can 
                offer them.”<br>
                <br>
                <img border="0" src="corp1.jpg" align="right" hspace="8" vspace="6" width="370" height="259">OOTO is a joint venture of Oiltanking Odfjell Oman (70 per cent 
                stake); Oman Oil Company (25 per cent) and a private investment 
                company (5 per cent). OOTO signed a contract for the operation 
                of liquid berthing facilities and the development of an 
                independent bulk liquids storage terminal at the Sohar 
                Industrial Port in November 2004. Oiltanking, a subsidiary of 
                Germany’s Marquard &amp; Bahls AG, is the second largest independent 
                storage provider worldwide for petroleum products, chemicals and 
                gases. Oiltanking owns and operates 62 terminals in 20 countries 
                in Europe, Middle-East, North and South America and Asia and has 
                an overall capacity of over 13 million cubic meters, Odfjell is 
                a leading company in the global market of transporting chemicals 
                and other specialty bulk liquids as well as providing related 
                logistical services. The fleet totals about 123 ships, trading 
                both globally and regionally.<br>
                <br>
                <b>Proven benefits</b><br>
                Independent storage terminals like OOTO perform vital connecting 
                services between the world’s oil fields and the users of 
                derivative products. Companies like Aromatics Oman benefit from 
                such terminals as they do not have to invest in additional 
                infrastructure like storage tanks and pipelines to pump the 
                liquids from the port to their facility and this helps them to 
                focus on their core business. The facilities of OOTO with its 
                large tank farm and liquid berths has become a hub of activity 
                for all the players in the port and has also attracted 
                international clientele, says Reumer who has nearly 18 years of 
                experience in the bulk liquid storage industry, having worked in 
                Singapore, South Africa, England and Rotterdam.<br>
                <br>
                During the last three decades, Oiltanking has become the world’s 
                second-largest company in this competitive field. Its clients 
                include private and state oil companies, refiners, petrochemical 
                companies, and traders in petroleum products and chemicals. 
                Oiltanking serves as a trustworthy custodian of valuable liquids 
                as well as for mixing of various liquids and offers the proven 
                ability to manage terminals profitably by developing 
                capital-intensive terminal facilities with substantial local 
                business partners.<br>
                <br>
                OOTO is committed to its Omanisation programme and has a 
                well-trained team of around 65 to operate the tank terminal as 
                well as the liquid jetties. Omanis represent the vast majority 
                of the workforce of the company. The company has 39 tanks to 
                store all kinds of liquids in various sizes with the smallest 
                tank having a capacity of 1700 m3 for chemicals and the largest 
                being a 55000 m3 tank for feedstock. </font> 
