                </font><b><font face="Verdana" size="4">THE HUMAN NETWORK<br>
            </font></b><font face="Verdana" size="1"><b>Corporate blogging 
            remains an also ran social medium in Oman, but it is a growth trend 
            that is here to stay. By Mayank Singh<br>
            </b><br>
            <img border="0" src="tech.jpg" align="left" hspace="8" vspace="6" width="300" height="197">Mahfood 
            al Wahaibi noticed a pizza cutter doing his chores without a head 
            net on one of his regular visits to Pizza Hut. On November 15, 2008, 
            8.18 am, he dashed off a protest mail to Pizza Hut, threatening to 
            take up the matter with the Muscat Municipality. The operations 
            manager of Pizza Hut was alerted and swift action ensured, Wahaibi 
            was thanked for bringing the anomaly to the managements notice on 
            November 17. Impressed Wahaib closed the matter saying, “Pizza hut 
            is not only for you but it’s for all. Pizza Hut is number 1.”<br>
            <br>
            It would be easy to dismiss the example as just another customer 
            compliant being met with alacrity, but for the fact that the entire 
            flow of communication was done on the Khimji Ramdas (KR) corporate 
            blog – Bright Sparks. Says Rajiv Ahuja, head, corporate 
            communication, KR, “The blog works as an Ombudsman for customers. 
            Second, the flow of communication on a blog is much faster.”<br>
            <br>
            <b>Nascent stage</b><br>
            Despite such apparent benefits of corporate blogging, only a handful 
            of organisations in Oman have tried their hand at the medium. Apart 
            from KR’s Bright Sparks, Shell Intilaaqah’s intilaaqah. blogspot and 
            the Public Establishment for Industrial Estates, peie.blogspot are 
            the only corporate blogs in Oman. And most of them 
            <img border="0" src="tech1.jpg" align="right" hspace="8" vspace="6" width="225" height="233">confess to the 
            less than optimum use of blogs as a social media. Says Abdullah al 
            Jufaili, director, Intilaaqah, “Most of the time we use our PR on 
            the blog and that is not the best utilisation of the tool. A blog 
            should engage the reader and provoke peoples thoughts.” Adds Dave 
            Pender, government advisor, PEIE and advisor, Oman Brand Mangement 
            Unit, “We would like to see more Omani companies blogging, but in 
            many cases companies feel it isn’t appropriate to their business. 
            But make no mistake, blogging is a growth trend and is definitely 
            here to stay.”<br>
            <br>
            A corporate blog gives a human face to a brand. It enables companies 
            to participate in online conversations, connect with customers. In 
            addition a company can put out its PR and organise competitions on a 
            blog. As a blog is a social media, it is important that companies 
            are open to the idea of indulging in a conversation with the user.<br>
            <br>
            Statistics prove that only 16 per cent of people trust corporate 
            blogs. Thus for a blog to gain credibility it is essential to win 
            over the trust of the customer. One way of doing this is by taking 
            out the ‘corporate’ from the corporate blog. “When you talk to 
            people on the blog it has to go beyond company speak,” says Ahuja.<br>
            <br>
            <b>Engaging the reader</b><br>
            <img border="0" src="tech2.jpg" align="left" hspace="8" vspace="6" width="225" height="226">The trick then is to observe, listen, learn and engage the customer. 
            And this can be done in various ways. Bright Sparks has been doing 
            this by increasing its offerings on the blog. The blog offers skin 
            care recommendations from Kaya Skin Clinic specialists, tips on 
            photography under the name Camasura (A combination of Camera and 
            Sura which means photography in Arabic). The blog has added funny 
            engaging creative ad films, business personalities, global brands 
            making waves and articles of interest from the media to enhance the 
            appeal of its blog. “As long as it’s done right, a good blog gets 
            people thinking and makes them more aware of your brand. It offers 
            businesses the freedom to communicate more candidly, whilst still 
            carefully crafting and controlling a marketing message; and a chance 
            to contribute to the dialogue of their sector, positioning the 
            business as a key player,” says Pender. <br>
            <br>
            Intilaaqah has plans to change the content of its blog this year. As 
            a start the blog tried out a pool by asking the question – ‘What do 
            you think about entrepreneurship programmes in the country’, the 
            query elicited responses from various countries in Europe and the 
            Middle East. Says Jufaili, “As our main focus is entrepreneurship 
            development, we can talk about what has been achieved successfully, 
            the sectors which offer promising returns for entrepreneurs etc on 
            the blog.” Intilaaqah has the distinction of being the only 
            corporate Arabic blog in Oman and Jufaili has plans of encouraging 
            Arabic writers on the blog.<br>
            <br>
            <img border="0" src="tech3.jpg" align="right" hspace="8" vspace="6" width="182" height="301">Though there is no sure shot formula for success, there are a few 
            principles that can come in handy – a blog should be updated 
            regularly rather than adding a bunch of posts at longer intervals. 
            Corporate blogs should have a human face, be honest, use a warm tone 
            of voice, admit genuine mistakes and correct them. It should be 
            discreet without volunteering information that can be potentially 
            harmful to the company’s interests. Negative remarks or rumours 
            about the company should be addressed immediately.<br>
            <br>
            There are a number of tangible benefits to be derived from blogs – 
            the new generation of web surfers go to blogs more than websites as 
            they are interested in knowing what is happening behind the scenes. 
            Technically, blogs are easier to update and keep track of as an 
            embedded RSS feed lets readers know if there have been any changes 
            on the blog. Says Pender, “Blogs are easier to update than the 
            company website and this is a major plus for many. And by adding 
            well written and interesting fresh content you can improve search 
            engine optimisation.”<br>
            <br>
            <b>Challenges on the way<br>
            </b>Despite such benefits, there are a number of reasons why 
            companies have not looked at corporate blogs as a viable 
            communication option. “The lack of awareness about the medium is the 
            biggest factor holding back the trend, some companies do not even 
            have a website let alone a blog,” says Jufaili. There are others who 
            think that maintaining a blog requires a lot of technical expertise 
            and hence have held back. Companies with a website may be at a loss 
            about differentiating a blog from their site.<br>
            <br>
            In today’s world not having a personality makes for a faceless 
            organisation. Such organisations have weak customer relationships, 
            low loyalty, negative public brand perception and a host of other 
            problems. A corporate blog goes a long way in giving a company a 
            humane and benign personality. </font></p>

