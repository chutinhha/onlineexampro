                    </font><b><font face="Verdana" size="4">The crowd-puller<br>
                </font></b><font face="Verdana" size="1"><b>Walter Hagen 
                single-handedly modernised the game of golf and went on to 
                became its greatest icon<br>
                </b><br>
                <img border="0" src="golf.jpg" align="left" hspace="8" vspace="6" width="175" height="448">What other golfers before him could not achieve, Walter Hagen 
                could do with ease. The Rochester-born golfer single handedly 
                modernised golf and became its greatest icon. He was the first 
                professional golfer to realise the commercial opportunities of 
                product endorsement. At the height of his popularity, he charged 
                huge money for exhibition matches. Apparently, he went on to 
                become the first golfer to earn and spend a million dollars. It 
                is reported that while staying in London, he hired a Rolls Royce 
                and stayed at the Savoy. He popularised colourful clothing on 
                the golf course. Hagen’s successes as a golfer includes four 
                Opens, two US Opens and five US PGA Championships.<br>
                <br>
                Hagen’s record of success in the 
                Majors speaks volumes about his talent as a golfer. Begging to 
                be different, he was not the one given to exhaustive 
                preparation, mentally, physically or otherwise. As a player, his 
                attitude was either superb or terrible.<br>
                <br>
                Hagen knew how to turn the table against his opponents. He used 
                to bank on psychology to exploit any weakness in his opponent. 
                He once invited the main competitor from a clubhouse to watch 
                him sink a putt on the 18th hole. He told him the next day that 
                he would beat him and lift the trophy. Luckily, he did.<br>
                <br>
                <b>Winning is a habit</b><br>
                His greatest accomplishment is winning five PGA Championships, 
                including four in a row. He won in 1921. However, he did not 
                take part in 1922 and lost to Gene Sarazen in 1923, then won 
                each year from 1924 to 1927. Less surprisingly, during those six 
                years of competition, he lost one match. Hagen won 11 
                professional majors, more than any golfer after Jack Nicklaus 
                and Tiger Woods. His first win in a major was the 1914 US Open, 
                at the age of 22. Hagen was inducted into the World Golf Hall of 
                Fame in 1974.<br>
                <br>
                Hagen fought to raise standards for pro-golfers. At a tournament 
                in England, he rented a Pierce-Arrow limousine, parked it in 
                front of the clubhouse and used it as a changing room after the 
                club refused him entry to its locker room. In fact, Hagen’s 
                presence at a tournament guaranteed great crowds.<br>
                <br>
                He was equally exciting and flamboyant off the course, earning 
                and spending money lavishly. Hagen often stayed at best hotels, 
                threw the best parties, and hired limousines to take him to 
                tournaments.<br>
                <br>
                Hagen’s place in golf’s hall of fame is deserved not only 
                because of his achievements but also because he brought to the 
                game a much needed sense of sport and fun. Professionals 
                everywhere are indebted to him. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
