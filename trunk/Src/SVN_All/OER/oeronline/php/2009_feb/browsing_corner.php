                <b>The book is a fervent appeal to the world to come to terms 
                with the environmental catastrophe we are fast moving towards, 
                writes Ganesh Sundararaman</b><br>
                <br>
                <img border="0" src="browse.jpg" align="left" hspace="8" vspace="6" width="175" height="375">His 
                language is sharp at times, almost shouting, about the fact that 
                many world bodies are aware of the problem, but not seized of 
                it! About the apathy generally observed, he laments, “Green is 
                still more an option than a necessity.”<br>
                <br>
                Much of the escalation, according to him, emerges from the 
                growing Americanism in the developing world, which seem to be 
                perpetrating the process of out produce and outspend – deeply 
                ingrained habits when resources are abundant. <br>
                <br>
                That the Americans are best equipped to stem the “Americums” 
                seems, at first, a veiled accusation. “We have lived too long on 
                borrowed time and borrowed dimes. We need to get back on our 
                country” is his call to America! But he establishes the dawn of 
                realisation on the US administration, and the legislations that 
                have since fallen into place in America.<br>
                <br>
                <b>Lack of willpower</b><br>
                But, the rest of the world is still just expressing a worry 
                about the problem, not doing much. To prove the point, Friedman 
                has travelled. From Doha to Dalian (China), Beijing, Peshawar, 
                Brazil, Guyana, Kenya, Tanzania, Egypt, the Rio Tambopata river 
                in Amazon, it’s a researcher’s job to track all the observations 
                Friedman has documented. Other issues directly contributing to a 
                rapidly diminishing clean world are succinctly enunciated by 
                Friedman in exclusive chapters – energy poverty, petro-politics, 
                the ecological logic of capitalism, and the energy-climate era.<br>
                <br>
                It’s not a dream to believe we can have clean electrons, ensure 
                that green is the new red, white &amp; blue. It’s just a matter of 
                innovating, generating and redeploying. Friedman’s illustrative 
                is simple. A little girl was asked to piece together torn bits 
                of a satellite photograph of the Earth. She did it in minutes. 
                “There was a face of a man on the other side of the paper. When 
                I put the face of the man together, the Earth got put together 
                too.”<br>
                <br>
                When we conserve one resource, we are actually protecting 
                another dependent element of nature. If the collective effort 
                moves in that direction, what a little girl had to say at the 
                end of Friedman’s book may yet sound like an appeal, not an 
                accusation - “At school, even in kindergarten, you teach us how 
                to behave in this world…then why do you go out and do all the 
                things you tell us not to do? You are deciding what kind of a 
                world we are growing up in. Parents should be able to comfort 
                their children by saying ‘everything is going to be alright’.
                <br>
                <br>
                But I don’t think you can say that to us anymore!” <br>
