                    </font><b><font face="Verdana" size="4">‘LET’S TALK AIDS’</font></b><font face="Verdana" size="1"><br>
                <b>Oman takes strides to combat AIDS. As a partner, committed to 
                doing its best to increase awareness of the dreaded disease, 
                Oman Economic Review presents a series of articles on the 
                campaign and the wonderful work undertaken by the people behind 
                the campaign. We will bring you real life stories, facts, 
                figures and all the information you need to help protect your 
                family and you, now and in the future. Let’s talk about it 
                without embarrassment and with candour. In this first article, 
                we present the Campaign ‘Let’s Talk AIDS’… </b><br>
                <br>
                The Sultanate has taken one more step to counter the growing 
                AIDS Pandemic with the launch of the ‘Let’s Talk AIDS’ campaign. 
                The two-year long national AIDS awareness campaign has been 
                launched under the auspices of HE Dr Ali Bin Mohammed Bin Moosa, 
                Minister of Health. At the launch ceremony, the Al Bustan Palace 
                (the venue of the inauguration) was overwhelmed with guests and 
                visitors, who joined hands to fight the deadly AIDS menace. A 
                scinti-llating performance by the Royal Oman Symphony Orchestra 
                marked the occasion. ‘Let’s talk AIDS’ is part of the Oman 
                National Response strategy for HIV/AIDS, 2008-2011 launched by 
                the National AIDS Programme and all UN agencies in Oman. <br>
                <br>
                It is believed that 100 new HIV/AIDS infected people are 
                registered in Oman every year. Half of the cases are among young 
                people aged 15 to 35 years, resulting in risky behaviour 
                affecting their health. The ‘Let’s Talk AIDS’ Campaign aims to 
                educate the masses on the numerous misconceptions on the 
                transmission of HIV/AIDS, despite the high level of awareness on 
                the disease and the main causes of its transmission. <br>
                <br>
                Awareness plans would be initiated through events like art 
                competitions, skits and other education and awareness raising 
                activities, through schools and groups of young people such as 
                Scouts and Guides.<br>
                <br>
                The campaign plans to use extensive media coverage over the 
                course of two years. There are also plans to conduct training 
                workshops for doctors, councillors, and the youth to raise 
                awareness on HIV/AIDS. A National AIDS Committee has been formed 
                which includes representatives from ten government ministries, 
                the private sector, academia, UNFPA, UNAIDS, UNICEF and WHO.<br>
                <br>
                Partners and sponsors can contribute by way of financial and 
                moral support to the campaign. <br>
                <br>
                <b>Let’s Talk AIDS</b><br>
                This is the theme for a two-year HIV/AIDS Social Communication 
                Campaign. This campaign is part of the Oman National Response 
                Strategy for HIV/AIDS, 2008-2011 launched by the National AIDS 
                Programme.<br>
                <br>
                <b>The campaign aims to</b>:<br>
                • Increase awareness on the prevention of HIV/AIDS and the 
                various HIV-related services available in Oman.<br>
                • Demonstrate solidarity with and advocate acceptance and 
                support to people living with HIV/AIDS.<br>
                <br>
                <b>Phases of the campaign</b>:<br>
                • Phase 1: HIV/AIDS in Oman<br>
                • Phase 2: Awareness of the ways of protecting yourself from 
                HIV/AIDS.<br>
                • Phase 3: Acceptance, support and compassion for people living 
                with HIV/AIDS.<br>
                • Phase 4: HIV/AIDS services in Oman.<br>
                <br>
                <b>Events and activities</b>:<br>
                • There will be extensive media coverage over the course of two 
                years (radio, television and print) in a bid to increase 
                awareness and reach out to the masses in every way possible. <br>
                • Numerous events including various youth-related activities 
                will be organised at the Muscat and Khareef Festivals.<br>
                • Events are planned throughout the year – these include art 
                competitions, skits and other education and awareness raising 
                activities through schools and groups of young people such as 
                Scouts and Guides.<br>
                • Training workshops for doctors, counsellors and youth to raise 
                awareness about HIV/AIDS will be conducted.<br>
                <br>
                <b>The Campaigners</b><br>
                The National AIDS Committee includes representatives from ten 
                government ministries, the private sector, academia, UNFPA, 
                UNAIDS, UNICEF and WHO.<br>
                <br>
                <b>Why?</b><br>
                • 100 new HIV/AIDS infected people are registered in Oman every 
                year.<br>
                • AIDS is a health problem that has negative social and economic 
                consequences in society.<br>
                • Half the cases are among young people aged 15 to 35 years, 
                resulting from risky behaviour affecting their health.<br>
                • Prevalence of numerous misconceptions on the transmission of 
                HIV/AIDS, and its main factors of transmission.<br>
                • Stigma and discrimination against people living with HIV/AIDS.<br>
                <br>
                <b>How you can help</b>: <br>
                Partners and sponsors are needed for the ‘Let’s Talk AIDS’ 
                campaign to ensure its success.<br>
                <br>
