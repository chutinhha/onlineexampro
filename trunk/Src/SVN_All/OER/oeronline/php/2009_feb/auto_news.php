                    <font face="Verdana">SAVING PRECIOUS METALS <br>
                    </font></font></b><font face="Verdana" size="1">Mazda Motor 
                    Corporation has announced the world’s first market 
                    application of single-nanocatalyst technology in automobile 
                    catalytic converters. This highly durable new catalyst 
                    significantly reduces the amount of precious metals used and 
                    effectively purifies vehicle exhaust gases. It will first be 
                    introduced in the all-new Mazda3 (known as the Mazda Axela 
                    in Japan) which will commence global sales this year. With 
                    the single-nanocatalyst, the underfloor catalytic converter 
                    in the all-new Mazda3 requires only 0.15g/L of precious 
                    metals, approximately 70 per cent less than the 0.55g/L 
                    required in the previous model. Along with the substantial 
                    reduction in precious metal usage, the Mazda3 continues to 
                    qualify as a Super Ultra-Low Emissions Vehicle (SU-LEV) in 
                    Japan by achieving exhaust emissions that are at least 75 
                    per cent cleaner than the government’s 2005 regulations. 
                    Going forward, Mazda will progressively introduce the 
                    single-nanocatalyst to all its global markets, which will 
                    contribute to a reduction in the consumption of rare metals 
                    and cleaner vehicle exhaust emissions.<br>
