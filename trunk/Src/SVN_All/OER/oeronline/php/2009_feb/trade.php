                    </font><font face="Verdana" size="4"><b>Doing business 
                differently</b></font><font face="Verdana" size="1"><br>
                <b>The Cultural and Economic Diplomacy Office of the Philippines 
                Embassy has been in the forefront of boosting trade and business 
                links with Oman, finds out Visvas Paul D Karra<br>
                </b><br>
                <img border="0" src="trade.jpg" align="left" hspace="8" vspace="6" width="300" height="213">In July 2008, Ahmed Saleh Al 
                Farsi, director general, Public Authority for Craft Industry (PACI), 
                led an Omani delegation on a visit to the Philippines. Then in 
                the last week of November 2008, Philippine representatives to 
                the 2008 World Halal Expo in Abu Dhabi made a stop over at 
                Muscat, as part of a Food Selling Mission. A number of similar 
                reciprocal bilateral missions to the Sultanate of Oman and 
                Philippines were made by officials of the two countries. All 
                these visits were coordinated by the Cultural and Economic 
                Diplomacy Office of Embassy of Philippines in Oman.<br>
                <br>
                Cultural diplomacy is the effective projection of a country’s 
                culture and cultural identity abroad through an integrated 
                cultural programme in order to enhance the country’s prestige in 
                the international community and thus strengthen international 
                understanding and cooperation. Economic diplomacy, on the other 
                hand, handles the advancement and promotion of the country’s 
                business interests and economic relations with the host country. 
                This involves expanding market access for products and services, 
                attracting offering investments, facilitating transfer of 
                appropriate technology and securing external resources needed 
                for economic and commercial representation. The Philippine 
                Embassy’s cultural and economic diplomacy office is tasked with 
                implementing the government’s development diplomacy which refers 
                to how the country conducts its foreign relations in order to 
                enhance its position in the vital areas of culture, trade, 
                investment, finance, tourism technology and aid, says Omar M 
                Pangarungan, head of the Cultural and Economic Diplomacy Office. 
                Pangarungan was deputy chief of the international economic 
                diplomacy office of the department of foreign affairs in the 
                Philippines prior to his posting in Muscat.<br>
                <br>
                The cultural and economic diplomacy office of the Philippines 
                has facilitated visits between government officials and private 
                businessmen of the Philippines and the Sultanate of Oman thus 
                increasing awareness on Philippine culture (through various 
                activities like food festivals, musical shows) and the 
                Philippine export products through the promotion of Philippine 
                international fairs and supporting trade related activities.<br>
                <br>
                <b>Channels for ties<br>
                <img border="0" src="trade1.jpg" align="right" hspace="8" vspace="6" width="172" height="292"></b>Efforts are being made to increase Philippine market share 
                in the Omani market through products display, food promotion 
                festivals, and active participation in trade missions and 
                international trade fairs aimed at increasing trade contacts 
                between Philippine-Omani businessmen. <br>
                <br>
                Pangarungan says that his department has rallied the support 
                from various Filipino associations/ organisations for the “Buy 
                Philippines Products Movement” which goes under the slogan 
                “Tangkilikin natin sariling atin”. “To this end and as mandated 
                by our government, the embassy, through the cultural and 
                economic diplomacy office, works side by side with Overseas 
                Foreign Workers (OFWs) in Oman to promote Filipino products in 
                the global market,” Pangarungan says.<br>
                <br>
                Pangarungan is grateful to the Philippine Mission Head 
                Ambassador Acmad D Omar whose active support and cooperation has 
                helped the Cultural and Economic Diplomacy department to pursue 
                its stated agenda with vigour. Ambassador Omar is also in 
                constant touch with His Excellency Khalil Al Khonji, chairman of 
                the Oman Chamber of Commerce and Industry, in order to encourage 
                the private sectors of both countries to play a more energetic 
                role to boost bilateral business relations between the two 
                countries, Pangarungan disclosed.<br>
                <br>
                <b>Avenues for cooperation<br>
                </b>Pangarungan expressed his appreciation for the existing 
                friendly relations between Philippines and Oman and said that 
                his country desires to deepen and further strengthen these ties 
                by seeking new avenues for further cooperation. The Philippines 
                government through its National Commission for the Culture and 
                the Arts (NCCA) is keen to have an agreement with Oman’s 
                Ministry of Culture and Heritage for cultural exchanges and open 
                possibilities for further cooperation and cultural exchanges, 
                says Pangarungan who is an advisor to the Philippines-Saudi 
                Arabia and Philippine-Bahrain Business Councils. He is also a 
                member of the board of directors of the Arab-Philippine 
                Foundation.<br>
                <br>
                Pangarungan also hoped that pending agreements could be 
                finalised at the soonest possible time with the needed action to 
                be taken by either side including an agreement between the 
                chambers of commerce of both the countries besides forming a 
                bilateral consultative commission in the near future. “I am 
                greatly fascinated by the natural beauty of Oman and inspired by 
                the inimitable leadership of His Majesty Sultan Qaboos bin Said 
                who, within a short period of time, transformed Oman into a 
                modern state while carefully preserving its heritage, culture 
                and traditions. This suggests that His Majesty The Sultan is one 
                of the great nation builders of the 20th and 21st centuries. His 
                reign of 38 years has brought his people to progress and 
                prosperity and gained for himself the honour of being an arbiter 
                of peace and justice,” Pangarungan observed.<br>
                <br>
                <b>Business visits<br>
                </b>A number of top ranking Philippine officials as well as 
                business delegations are expected to visit Oman during the 
                course of this year in order to boost the economic, political 
                and socio-cultural relations between Oman and Philippines. A 
                visit is being planned by the Philippine Chamber of Commerce and 
                Industry to enhance cooperation between the private sectors of 
                the two countries and enter into a chamber to chamber 
                cooperation with the Oman Chamber of Commerce and Industry to 
                intensify relations in various aspects.<br>
                <br>
                Top Filipino entrepreneurs are also expected to come to Oman in 
                June 2009 under the banner of GO NEGOSYO to raise awareness of 
                investment opportunities among OFWs. Francis G Estrada, 
                president of Asian Institute of Management (AIM); and Greg 
                Aguilar, chairman, Global Resources Energy Group Inc are some of 
                the noted Filipinos who will be heading various business 
                delegations to Oman in the coming months.<br>
                <br>
                “These business calls will hopefully generate enough interest 
                among Omani businessmen to invest and do business in the 
                Philippines,” feels Pangarungan.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
