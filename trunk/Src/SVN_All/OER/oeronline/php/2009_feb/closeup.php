                <p align="justify" style="margin: 3px 10px"><b>
                <font face="Verdana" size="4">The time is now</font></b><font face="Verdana" size="1"><br>
                <b>GCC states should vigorously pursue FTA with EU as such an 
                agreement will give GCC products unhindered access to one of the 
                most powerful markets in the world giving a fillip to bilateral 
                trade </b></font></p>
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>
                <br>
                </b></font><b>
				<font face="Verdana" size="1" color="#CC3333">By Dr Jasim Husain 
				Ali</font></b><font face="Verdana" size="1"><br>
                <br>
                <img border="0" src="close.jpg" align="right" hspace="8" vspace="6" width="125" height="148">It 
                is unfortunate to witness the proposed free trade accord between 
                the Gulf Cooperation Council (GCC) and 27-member European Union 
                (EU) faltering after so many years of hard-fought negotiations. 
                Purportedly, the trouble relates to EU’s lack of desire to sign 
                a deal that fails to meet all its conditions, not all of which 
                are economic
                in nature.<br>
                <br>
                In late 2008, GCC’s general-secretary Abdul Rahman al Attiyah 
                announced suspensions of negotiations until further notice. He 
                stipulated EU signing a draft accord as a condition for 
                resumption of talks. “We have informed the European side of the 
                suspension of negotiations on a free trade accord,” said GCC’s 
                secretary-general Abdul Rahman Al Attiyah.<br>
                <br>
                The extraordinary development reflects growing disappointment 
                within the GCC’s general-secretariat in Riyadh about prospects 
                of signing an accord with the EU. Attiyah and other GCC 
                officials had high hopes of reaching a deal during the French 
                presidency of European Union. President Nicolas Sarkozy enjoys 
                special ties with the Qatari Emir Shaikh Hamad bin Khalifa al 
                Thani. Both France and Qatar surrendered their respective 
                presidency of the EU and GCC by year-end 2008. The Czech 
                Republic assumed presidency for the first time since joining the 
                EU in 2004. Yet, Oman took over the rotating presidency of GCC 
                by virtue of hosting the group’s 29th annual summit towards end 
                of last year.<br>
                <br>
                <b>
                <img border="0" src="close1.jpg" align="left" hspace="8" vspace="6" width="173" height="358">FTA benefits<br>
                </b>The fact of the matter is that the GCC needs a trade pact with 
                the EU for several reasons. For one, an accord could help in 
                ending the trade imbalance, as evidenced by the European side 
                enjoying a surplus of some $15bn in 2007. Still, EU remains a 
                leading trading partner for GCC, though the opposite is not 
                true.<br>
                <br>
                Second, a trade pact gives the six-nation GCC unrestricted 
                access to 27 economies in Europe, the largest single economic 
                bloc in the world. The EU includes mighty economies like 
                Germany, France, Italy and the UK. In other words, it makes 
                sense for the EU to place some conditions on parties seeking 
                unhindered access
                to its markets.<br>
                <br>
                <b>Differing positions<br>
                </b>Traditional EU demands for ending the talks focused on human 
                rights and certainly democracy. More recently, the EU raised the 
                issue of environmental protection in the GCC as a precondition 
                to making further progress. Still, there are outstanding trade 
                matters. In 2007, EU Trade Commissioner Peter Mandelson urged 
                for progress on four issues, namely market access, rules of 
                origin, government procurement and application of the investment 
                protection and guarantees criteria within the GCC. The European 
                side pressed for unrestricted access to numerous investment 
                opportunities including the crucial energy sector.<br>
                <br>
                There is no consensus within the GCC states with respect to 
                foreign investment in oil and gas. For example, Kuwait is yet to 
                decide on allowing foreign participation in the development of 
                its oil fields. The scheme, known as Project Kuwait, aims at 
                generating an additional 450,000 barrels per day from four 
                fields located in northern region of the country.<br>
                <br>
                The development requires an investment of $7bn over a 20-year 
                period. Kuwaiti parliament continues to block the proposal on 
                the grounds that the constitution bars foreign ownership of the 
                country’s hydrocarbon resources. Conversely, Qatar has been 
                welcoming oil majors through production and sharing agreements. 
                International oil companies are credited for developing the 
                country’s petroleum sector particularly gas. Nevertheless, there 
                is no GCC agreement on foreign ownership.<br>
                <br>
                On its part, the GCC would like to see the removal of customs 
                charges on aluminum and petrochemical products. At the moment, 
                the EU imposes a six per cent customs duty on imports of 
                aluminum from the GCC. But the EU remains wary of governmental 
                support, such as under-priced gas to GCC producers, a matter 
                that grants them unfair advantage against their European 
                counterparts.<br>
                <br>
                <b>Protracted negotiations<br>
                </b>The trouble is that many GCC officials wrongly argue that 
                bilateral talks started some 20 years ago, or specifically in 
                1988. However, real talks between the two sides commenced in 
                2003 after the GCC became a customs union. For several years, EU 
                officials argued that they could not have an accord with a group 
                that lacks coordination on policies and procedures. It is argued 
                that the EU position paved the way for concerted GCC efforts to 
                integrate their economies.<br>
                <br>
                The customs union project went into effect in 2003. In 
                particular, the project calls for a unified external trade 
                policy amongst member states. In addition, at the start of 2008, 
                the GCC states commenced the Gulf Common Market (GCM). The 
                project aims at unifying the regional market through which 
                nationals benefit from available opportunities within the bloc. 
                In particular, GCM relates to free flow of factors of production 
                amongst member states. It covers all economic and investment 
                services, dealings in the stock market and setting up of 
                companies in the public and private sectors besides social 
                insurance among GCC citizens.<br>
                <br>
                Still, GCC states (except for Oman) seem determined to press 
                ahead with implementing another move to further integrate their 
                economies. The 29th summit held in Muscat in late 2008 addressed 
                key outstanding issues designed to implementing the monetary 
                union project by due date of 2010. The summit adopted the accord 
                including legal and organisational framework. The next step 
                requires member countries to endorse the agreement, which is 
                expected anyway.<br>
                <br>
                Implementation of monetary union project requires member 
                countries adhering to five conditions dealing with fiscal and 
                monetary standards, namely limiting public debt to 60 per cent 
                of gross domestic product (GDP); restricting budget deficit to 
                three per cent of the GDP; limiting inflation rate plus two per 
                cent; ascertaining that short-term interest rate not exceeding 2 
                per cent the average of three lowest rates of member countries; 
                and ensuring that reserves cover import bills of four months.<br>
                <br>
                In conclusion, GCC countries are at their highest level of 
                integrating economies (started carrying out customs union and 
                common market projects and plan to implement monetary union 
                initiative in 2010). Clearly, the EU has no justification for 
                delaying signing the long-awaited FTA with the GCC.
