            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">THE ‘WINNER’ TELLS THE ‘WINNERS-TO-BE’</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>Dr Amer Rawas won the NTI BizPro Business Leader of the year in 
            2008. His musings about the win last year offer a number of pointers 
            for people who wish to be ‘on the top rung’ in the leadership ladder<br>
            </b>
            <br>
            <b>
            <img border="0" src="news3.jpg" align="right" hspace="8" vspace="6" width="200" height="183">The NTI BizPro awards</b><br>
            Instituted by the National Training Institute, and supported by 
            Renaissance Services, BizPro Awards have been able to inspire 
            industry people treading the way towards establishing themselves as 
            business pioneers. The Awards have paved the way to recognise 
            talented Omanis who have the innate ability to steer the business 
            world. NTI is once again gearing up to set a similar stage for 
            brilliance in professionalism. <br>
            <br>
            “The most important thing I realised after I won the award was the 
            pride my people in Oman Mobile had in me. It is one thing to be a 
            boss, but to be treated as a leader, in the organisation you work 
            with, raises adrenalin to a different level altogether. At the same 
            time, it was a wonderful revelation that external partners, with 
            whom I had almost fought bitterly on some issues, also expressed 
            their delight on my being conferred the BizPro Business Leader 
            Award”, he said.<br>
            <br>
            Getting down to the role of leaders, Dr Rawas was emphatic, “In the 
            conducive economic environment we have in Oman, the downturn 
            notwithstanding, a true business leader would enthuse other leaders 
            to emerge. The government has ensured that every player should face 
            others in the same business because there is space. It’s good as a 
            business climate to perform in, and it’s good for the consumers. 
            Healthy competition stimulates innovative thinking, and the end 
            result is always positive. If just ‘living one’s job’ were to be 
            everyone’s objective in life, the economy will stagnate. It is our 
            collective responsibility to ensure that we stimulate one another, 
            to bring about all round excellence.”<br>
            <br>
            <b>Gradual growth paths create enduring success scope for businesses</b><br>
            <img border="0" src="news4.jpg" align="left" hspace="8" vspace="6" width="200" height="213">“For it to work,” he cautioned, “we need to temper our ambitions to 
            provide more opportunities to more people. Much has been written 
            about anger management and conflict management. No management guru 
            has thought of writing on a much-needed leader skill – ‘greed 
            management’. If we could build a steady growth path for our 
            businesses, and not always tune ourselves to the fast track 
            approach, everybody in the region would benefit by it”.<br>
            <br>
            Secondly, he says, “It’s important that the way is paved for new 
            leaders to emerge. Seniors must nurture talents in others, create 
            their own exit mechanism, and become dispensable. Leaders can grow 
            others in the organisation to be leaders, by providing the space for 
            them to exercise independent thinking. <br>
            <br>
            <b>When leaders maintain their composure in trying circumstances, 
            ‘business-as-usual’ is possible</b><br>
            The third behaviour Dr Rawas recommended of leaders was “the 
            avoidance of panic in any unforeseen circumstance. Your face may not 
            show it, but your behaviour will, and everyone in your organisation 
            will feel it. The demoralising effect is unimaginable’. <br>
            <br>
            <b>Learnings create inner strengths that drive innovation</b><br>
            About the power that leaders acquire naturally, Dr Rawas felt that 
            it should be channelised into calculated risks. Leaders should 
            ensure that the power in their hands is never abused. Have the 
            courage to venture into uncertain areas, take calculated risks. 
            There may be criticism, but if your fundamentals are right, nothing 
            wrong in “rocking the boat”.<br>
            <br>
            “Rising leaders have the potential energy to observe the 
            environment. The time difference between taking an elevator to the 
            top, and taking the escalator is not much. But as you go up the 
            escalator, you observe so much, you feel more secure knowing what 
            you are going through reaching the top. That’s a knowledge base that 
            infuses a courage of conviction”, was Dr Rawas’ reach-out to the 
            Young Achievers. </font></p>

