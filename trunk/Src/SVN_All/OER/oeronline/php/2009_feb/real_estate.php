                    <font face="Verdana" size="1"><b>Iskan Oman Investment 
                    Company has a number of factors like a strong shareholder 
                    base, innovative ideas and propitious timing working in its 
                    favour. An OER report</b><br>
                    <br>
                    <img border="0" src="real1.jpg" align="left" hspace="8" vspace="6" width="174" height="174">A group of Kuwaiti investors 
                    carried out a survey of various GCC countries last year to 
                    zero in on a place to start a venture. Based on the results 
                    they decided to enter Oman as the regulatory regime was 
                    found to be better and the Sultanate was insulated from 
                    global cross currents. Iskan Oman Investment Company (IOIC) 
                    was formed in the first quarter 2008 with a paid-up capital 
                    of RO25mn. Its mission is to offer a comprehensive range of 
                    real estate development projects and services targeting 
                    medium to high class customers. “Iskan Oman is conceived 
                    with a long term investment view to contribute to the growth 
                    and share the prosperity of the Sultanate’s economy,” says 
                    Adil Al-Bader, chairman, Iskan Oman. Its main objective is 
                    to develop a business model based on core and non-core 
                    business activities to provide real estate investment and 
                    related services under one roof.
                    <img border="0" src="real2.jpg" align="right" hspace="8" vspace="6" width="174" height="171"><br>
                    <br>
                    IOIC enjoys a financially strong, well reputable and 
                    diversified list of shareholders. Its shareholders include 
                    prominent financial institutions, government pension funds, 
                    high networth individuals and companies from Kuwait and the 
                    Sultanate of Oman. One major shareholder is Iskan Housing 
                    Finance Company (ISKAN) headed by HH Sheikh Abdullah Jaber 
                    Al Ahmad Al Sabah and the Kuwait Pension Fund. Its Omani 
                    shareholders include the Ministry of Defence Pension Fund, 
                    Public Authority of Social Insurance, Royal Office Pension 
                    Fund, Royal Guard of Oman Pension Fund, Dhofar International 
                    Development and Investment Holding Company, Al-Tamam Trading 
                    Establishment, Oman and Emirates Investment Holding Company. 
                    Says Dr Rashid Ali Al Balushi, managing director, IOIC, “We 
                    have a strong shareholder base and are looking at long term 
                    projects.”<br>
                    <br>
                    IOIC plans to be a one stop solution for all real estate 
                    needs of clients looking 
                    <b>
                    <img border="0" src="real3.jpg" align="left" hspace="8" vspace="6" width="175" height="250"></b>for at investment in the country. 
                    It is also open to the idea of enhancing its gamut of 
                    offerings from real estate core services to other non core 
                    activities as well. The company will join hands with foreign 
                    companies to bring in expertise and technology and provide 
                    excellent grade services and value for money to clients. 
                    Says Al-Bader “We are present in Oman to participate in the 
                    development of the economy and share our knowledge in long 
                    term projects.”<br>
                    <br>
                    <b>Innate strengths </b><br>
                    Being a start up the company is well positioned to make the 
                    most of prevailing market conditions. Says Al Balushi, “We 
                    were careful in utilising our capital during the period of 
                    high prices, this strategy is helping us now when real 
                    estate and construction prices have softened as we are able 
                    to offer better projects at lower cost.” The company is 
                    looking at capitalising on the current market situation by 
                    seizing the opportunity to buy land at lower prices and 
                    making the most of falling construction costs compared to 
                    the on-going projects. IOIC will also come up with unique, 
                    modern and first of its kind projects at affordable prices 
                    to attract investors. “We are working on introducing green 
                    buildings and new facilities for our customers,” says Al 
                    Balushi. The company is open to the idea of diversifying 
                    into different sectors to capitalise on available market 
                    opportunities. As a company IOIC, has set its sight on 
                    becoming a leader in the field of real estate investments in 
                    Oman. As they say well begun is half done.
