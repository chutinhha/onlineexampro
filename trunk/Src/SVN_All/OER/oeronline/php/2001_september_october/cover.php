                                <p><font face="Verdana" size="2">The Formula One Championship runs from March to October each year. Eleven Grand Prix teams with two cars each take part in the championship, competing in 17 races that are staged in 15 countries.<br>
                                The 22 drivers are all in the race for the World Driver's Championship, which is determined over the course of a season. Points are awarded to the top six place-getters in each race: 10 points for a win, 6 for second, 4 for third, 3 for fourth, 2 for fifth and 1 for sixth. Only the top three place-getters earn the honour of standing on the winners' podium after each race and spraying each other with massive bottles of champagne.<br>
                                The Constructor's Title is determined by the overall success of the 11 teams, with points awarded to each car on the same basis as the drivers.<br>
                                The art of Formula One is not just about getting around the track fastest. It is also a strategic game, with the timing of fuel pit stops crucial to the outcome of each race.&nbsp;<br>
                                There are two governing bodies within the sport. The most important organisation is The Federation Internationale d'Automobile
                                (FIA). They are the governing and sanctions body for the FIA World Driver's Championship, which is run to a set of technical and procedural regulations and specifications. Then there is the Formula One Constructor's Association
                                (FOCA), which is an organisation of the chassis builders who design and build the cars that race in the championship.</font><font face="MS Sans Serif" size="1"><br>
