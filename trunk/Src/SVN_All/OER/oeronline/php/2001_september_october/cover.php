                  <td width="100%"><img border="0" src="../../images/2001_september_october/formula1.jpg" align="left" hspace="5" vspace="5" width="218" height="218"><font face="Verdana" size="4" color="#FF0000"><font face="Verdana" size="2" color="#C0C0C0">::

                    Cover Story<br>

                    </font></font>

                    <font face="Verdana" size="4" color="#FF0000">Bahrain's winning formula</font>

                    <p><font face="Verdana" size="1"><b>Formula One motor racing is watched by more people each year than any other sport. No wonder Bahrain is so keen to hold the first Grand Prix in the Gulf. OER's Bahrain correspondent Nabs Al Busaidi reports</b></font></p>

                    <p><font face="Verdana" size="2">Formula One is the fastest, most exhilarating and sophisticated sport in the world. Racing at top speeds of over 200mph, Formula One cars represent the very summit of automotive technology, while their fearless drivers are international stars who influence and excite the imaginations of fans around the globe.&nbsp;<br>
                    Every two weeks, from March till late October, an estimated 350 million viewers in 150 countries watch 22 cars racing at one of 17 circuits around the world. With a cumulative annual audience of more than five billion, Formula One is the most-watched sport on television. Only the Olympics and the Soccer World Cup attract more viewers, and they take place only once every four years.<br>
                    Grand Prix are not only a valuable research and development laboratory for the world's leading car manufacturers, they are also a sensational sporting spectacle. Competition between drivers and teams is intense, and there is a super-charged atmosphere at every race.&nbsp;<br>
                    For all these reasons, Formula One has come to be associated not only with sporting distinction but also with glamour, prestige and big business. The rewards for those associated with Formula One, whether the teams, the sponsors or the host countries, have become highly lucrative in recent years.&nbsp;<br>
                    No wonder, then, that the Gulf is keen to get in on the action.<br>
                    Bahrain has unveiled plans to build the Gulf's first major motor racing track, and aims to hold a Formula One Grand Prix and other international racing events by August 2004.&nbsp;<br>
                    In July Bahrain signed an agreement on designing the facility with Tilke, a world leading motor racing circuits design and consultancy firm from Germany.&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_september_october/formula2.jpg" align="right" hspace="5" vspace="5" width="275" height="253">The plans call for three circuits - Grand Prix, inner and outer - and a drag track. The inner and outer track combined will form the Grand Prix loop, which will have a total length of 5.271 kilometres. It will have a maximum uphill slope of 6.4 per cent and a maximum downhill slope of 6.2 per cent. The estimated lap time for a Formula One car on the circuit is 1:35:51 at an average speed of 196.73kph. The project will take 14 to 18 months to complete and work is likely to start early next year.&nbsp;<br>
                    Details of the BD30 million (US$80 million) investment were announced by Sheikh Fawaz bin Mohammed Al Khalifa, president of the General Organisation for Youth and Sports. He said the Government had decided to go ahead with the track because it saw a number of benefits for the country's economy.&nbsp;<br>
                    The Government will now begin to explore the possibility of private sector involvement in the track. According to one ex international rally and racing driver, resident in Bahrain, the project could cost as much as $100 million.<br>
                    <br>
                    <b>Why Bahrain?</b><br>
                    <br>
                    Bahrain has much to offer the keen investor and has many notable attractions, but nothing on the size and scale of a Grand Prix. If Bahrain is looking to expand its global image, then staging a Formula One Grand Prix is about the best way to do it.&nbsp;<br>
                    Things started to change in Bahrain when Sheikh Hamad bin Isa Al-Khalifa took over as head of state after the death of his father in 1999. The new Emir has introduced a number of economic reforms and is looking at ways to build up Bahrain's tourism and banking industries - in direct competition with Dubai.<br>
                    As part of his research into changing the image of Bahrain, the Emir and two of his ministers visited the Italian Grand Prix in 1999 and met with Formula One boss Bernie Ecclestone.&nbsp;<br>
                    Staging a Grand Prix is a means for Bahrain to enter the largely untapped Gulf sports tourism market. It expects at least 50,000 spectators to flock to each high-adrenaline, high-octane, incident-packed race. (Existing Grand Prix attract well over 100,000 race day spectators.)&nbsp;<br>
                    There will also be other racing events, which will attract more spectators on a regular basis, giving a boost to the country's tourism and hospitality industries.<br>
                    Bahrain's already stable infrastructure will be expanded as international visitor numbers rise. More hotels, restaurants, leisure facilities, roads and other supporting industries will be required to cater for the Grand Prix and the ensuing increase in Bahrain's popularity.&nbsp;<br>
                    Millions who were once confused about the location of Bahrain will be forced to take note of the little island in the Arabian Gulf. In short, Bahrain will be put firmly and squarely on the world map.<br>
                    There are advantages for Formula One too. It wants to expand its global image by gaining a foothold in the Middle East, and there is no other location with the exotic undertones that Bahrain has to offer.&nbsp;<br>
                    The mysticism of the Middle East is embodied in Bahrain, which is untouched by any other pan-global sporting event.&nbsp;<br>
                    Bahrain has the financial muscle to build the amenities that Formula One requires, and there are endless potential sponsors in the financial sector.&nbsp;<br>
                    Bahrain is also a gateway to the Middle East, with the Saudi Causeway serving as a link to the mainland. Through this gateway many other industries will pass, more than happy to reap the benefits of association with Formula One.&nbsp;<br>
                    On a more practical note, Bahrain will serve as a warm-weather testing ground on the doorstep of other Grand Prix hosts Malaysia, Japan and Australia.&nbsp;<br>
                    If the plans come to fruition, Bahrain and Formula One will enjoy a happy alliance that could be of enormous benefit to the Gulf, focusing world attention on the region and creating new business opportunities.<br>
                    <br>
                    <b>Bahrain Leads the Pack</b><br>
                    <br>
                    A number of Middle East countries, such as Egypt, Lebanon and Dubai, are already working on plans to build motor racing tracks of a similar nature. But these tracks, rather than hindering, could help one another in several ways, particularly by enabling a Middle East championship and by raising the standard of regional drivers.<br>
                    However, Bahrain hopes it will be the only host for Formula One racing, and there are several factors in its favour over the more obvious Dubai.<br>
                    Perhaps most importantly, Ecclestone - Mr Formula One - is thought to favour Bahrain as the Middle East franchise expansion location. The project in Bahrain has royal backing, with Crown Prince Sheikh Salman bin Hamad Al Khalifa a big fan and very involved in the proceedings. Also, the Dubai bid is expatriate driven, whereas the Bahrain project is locally generated, and this is seen as a major factor.&nbsp;<br>
                    Time is also on Bahrain's side. It has stolen a march on the others by moving first, and no other country in the region has come so far.<br>
                    <br>
                    <b>Formula One - a sport and a business</b><br>
                    <br>
                    Formula One is all about the world's fastest racing drivers and cars competing for motor sport's ultimate prizes - the World Driver's Championship and the Constructor's Title. But Formula One is also where sport meets technology for the business of entertainment.&nbsp;<br>
                    The sport has changed dramatically during the 1990s due to the power of money.&nbsp;<br>
                    Grand Prix can generate more than US$100 million in direct income, not to mention what the host country gains in terms of tourism revenue and growth in its service industries.&nbsp;<br>
                    The racing itself is driven by a relentless search for fractional improvements in lap times. In order to achieve this, tens of millions of dollars are required for research and development, just to reduce a lap time by a mere 0.1 second. The middle ranking teams need to raise between $25 million and $35 million each year in sponsorship to compete, whereas a top team is likely to spend many times that amount. Fiat - Farrari's parent company - reportedly spends $285 million a year on its Formula One team.<br>
                    Blue-chip companies seem to be the only ones that can afford to become 'title sponsors' of the sport, and they do so because each race is seen by an average of 350 million potential customers on TV screens around the world.<br>
                    One significant pointer that shows the development of the sport in recent years is the increasing number of television viewers. Total annual viewership has risen from 17.5 billion at the turn of the decade to 57 billion in 1999 - a figure arrived at by counting each time someone tunes in to a Formula One broadcast.&nbsp;<br>
                    The popularity of the sport is growing fast, especially in Asian countries. British television audiences have grown by 43 per cent in five years, and the number of women viewers has risen by around 10 per cent since 1994. The FIA estimates that 38 per cent of all Formula One viewers today are women.<br>
                    There are many reasons for the popularity of the sport. Some fans are still attracted to its long and distinguished history; others tune in because it is such a fast and furious, colourful and technology-driven sport. Today, almost anything that happens in Formula One, whether controversial, political or otherwise, makes world headlines, proving the rule that all news is good news.<br>
                    Formula One is also attracting new and varied sponsors. In addition to the companies that are traditionally associated with the sport, such as tobacco manufacturers, car manufacturers and fuel suppliers, Formula One now counts fashion houses, airlines, finance companies, telecommunications corporations and computer manufacturers among its backers.<br>
                    The growth of Formula One must be regarded as one of the most stunning business success stories of recent history. Marketing campaigns associated with Formula One have an immediate global impact, transcending geographic, language and social barriers, together with a universally recognised image of excitement, precision, teamwork and competitiveness.&nbsp;<br>
                    In the future, Bahrain might no longer be remembered for the discovery of oil in the Middle East, nor have to rely upon its natural pearl diving. In time it could be remembered as a location for one of the most exciting Grand Prix in the modern era.&nbsp;</font></p>

