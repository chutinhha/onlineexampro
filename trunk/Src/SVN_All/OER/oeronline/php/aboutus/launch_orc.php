              <p align="justify"><font face="Verdana" size="1"><br>
              The Oman Refinery Company (ORC) has launched its website on the 
              World Wide Web. The website URL is 
              <a target="_blank" href="http://WWW.ORC.CO.OM">WWW.ORC.CO.OM</a>. The neatly 
              designed and easy-to-navigate website offers the visitor a 
              detailed insight into the company's activities. <br>
              <br>
              “The prime objective of the website is to open channels of 
              communication between the company and its internal and external 
              communities,” said Mr Salim Bin Awadh Al Rubki, Manager – 
              Administration and Public Relations of Oman Refinery Company.<br>
              <br>
              “The site hosts information on the various aspects of the company. 
              The main sections covered on the site are Company Profile and 
              Facts, Business, QHSE, HR Practices and Records. The website can 
              be used to contact ORC using the feedback form. There is also a 
              section devoted to press releases. In addition, the website 
              outlines details of the Company’s involvement in the community and 
              the environmental management systems.” Mr. Rubki added.<br>
              <br>
              The site offers fast download, a vibrant look and feel and has 
              dynamic functions that enable easy access from the home page. A 
              unique feature of the website is its ‘one-click’ accessibility 
              that enables quick browsing between sections and return to home 
              page.<br>
              <br>
              The site is hosted on a high-end server that ensures total 
              security and a 99 percent uptime. The website uses embedded 
              server-side technology which is universally accepted as the best 
              across websites the world over. It has been developed by UMS 
              interactive, the leading web and digital branding company of Oman.<br>
              <br>
              The Oman Refinery Company ranks as a milestone among the historic 
              successes of Oman's development. Besides providing employment to 
              341 people; of whom over 90% are Omanis, the Refinery supplies the 
              entire fuel needed in the Sultanate. ORC is utilizing national 
              resources optimally to provide impetus to the overall growth and 
              prosperity of the people of the Sultanate of Oman.<br>
