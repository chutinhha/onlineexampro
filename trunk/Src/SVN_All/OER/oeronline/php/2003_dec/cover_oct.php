              <p align="left"><font face="Verdana" size="1"><b>AACO's 36th AGM 
              concludes successfully </b><br>
              The annual general meeting of Arab Air Carriers Organisation (AACO), 
              which was hosted for the first time in Oman this year, concluded 
              successfully after approving a set of important resolutions, most 
              importantly deciding on general guidelines about the future 
              dealings with Europe and the launching of a business plan to build 
              an Arab Portal. <br>
              The theme of the 36th AGM, which was inaugurated by HE Malik bin 
              Suleiman Al Maamari, the Sultanate's minister of transport and 
              communications, was 'Timeless Oman'. The 3-day AGM (October 6 - 8, 
              2003) was hosted by Oman Air and was presided by its chief 
              executive and AACO President Abdul Rahman Al-Busaidy. <br>
              <br>
              <b>Oman Oil: Transforming the landscape </b><br>
              Few would deny that Oman Oil Company (OOC) has probably had the 
              most eventful 2003 among companies in the Sultanate. It has 
              transformed an oil marketing company, helped to lay the 
              foundations for the Oman India Fertiliser project at Sur and has 
              started work on the Sohar Refinery. Its portfolio has also 
              encompassed developing a further two petro-chemical projects, an 
              aluminium smelter and a polypropylene project, all part of the new 
              industrial complex and port facilities also planned in Sohar. <br>
              First and foremost were the changes at Oman Oil Marketing Company. 
              The re-branded company was officially launched in October and 
              formally opened its first service station in its new insignia in 
              the same month. The plan to phase out all existing 77 BP service 
              stations is underway with re-branding of sites within the Muscat 
              region set to be complete by the end of December. Retrofit of the 
              remaining nationwide sites is set to be completed by May 31, 2004.
              <br>
              <img border="0" src="om.jpg" align="right" hspace="6" vspace="6" width="200" height="147">Another major milestone which was staged in October was the 
              ceremony held by Oman-India Fertiliser Company (Omifco) to unveil 
              the foundation stone of its fertiliser plant in Sur. A strategic 
              project for both Oman and India, Omifco's plant is intended to 
              create a sustained source for fertilisers at competitive prices to 
              meet India's rising requirements for at least the next 15 years.
              <br>
              The $969 million (RO373 million) ultra-modern unit is scheduled to 
              go on stream in the third quarter of 2005 and will produce 1.6 
              million tonnes of granulated urea and 248,000 tonnes of surplus 
              ammonia using natural gas as feedstock.<br>
              Meanwhile, October also signalled the commencement of work by 
              Japan's JGC Corporation to develop Sohar Refinery, a $1 billion 
              (RO385 million) project. <br>
              The refinery is a wholly-owned government company, represented by 
              the Ministry of Finance holding an 80 per cent stake while the 
              remaining 20 per cent shareholding is held by OOC. <br>
              The refinery, which will have a crude unit with a capacity of 
              116,400 barrels per day and a residue fluid catalytic cracking 
              unit (RFCCU) with a capacity of 75,260 barrels per day, should be 
              completed by the third quarter of 2006. </font></p>
            <p><font face="Verdana" size="1"><b>Suhail Bahwan 
            acquires majority stake in NBO <br>
            <img border="0" src="nbo.jpg" align="left" hspace="6" vspace="6" width="200" height="152"></b>National Bank of Oman (NBO) received a much-needed boost when 
            its shareholders unanimously approved an equity private placement by 
            Oman's Suhail Bahwan Group (SBG), one of the country's largest 
            business conglomerates. <br>
            The move meant that one of Oman's most powerful businessmen, Suhail 
            Bahwan, picked up a 34.5 per cent controlling stake in NBO, the 
            country's second largest commercial bank by revenue. Upon completion 
            of these arrangements, NBO's authorised share capital increased to 
            RO80 million from RO50 million. <br>
            During recent months before the deal was announced, the management 
            of NBO had undertaken a rigorous review of its total loan portfolio 
            across Oman, the UAE and Egypt.<br>
            The review indicated that classified advances of NBO are projected 
            to increase by 28 per cent to reach RO271 million during the fourth 
            quarter of 2003, and that this would create a need for an increase 
            in related provisions of RO50 million. After taking such a high 
            level of provisions in order to complete the exercise of &quot;cleaning 
            up&quot; the loan portfolio, it is envisaged that NBO will be report an 
            operating loss for 2003 of RO52 million.</font></p>
            <p align="left"><font face="Verdana" size="1"><b>Air Arabia 
            celebrates inaugural flights<br>
            </b>Air Arabia LLC, the UAE's third national airline and first 
            low-cost airline of its kind in the Middle East &amp; North Africa 
            region, celebrated its inaugural flights to Bahrain, Kuwait, 
            Damascus, Muscat and Beirut. <br>
            Air Arabia was established in February 2003 by an Ameeri decree 
            issued by his highness Dr Sheikh Sultan Bin Mohamed Al Qasimi, the 
            Ruler of Sharjah and Member of the Supreme Council of the United 
            Arab Emirates. <br>
            Air Arabia is modelled after leading American and European low-cost 
            airlines and is customised to local preferences. Its main focus is 
            to make air travel more convenient and frequent through Internet 
            booking and offering the lowest fares in the market without 
            sacrificing on service and safety standards. </font></p>
            <p><font face="Verdana" size="1"><b>Golf project on 
            course </b><br>
            <img border="0" src="scan5.jpg" align="left" hspace="5" vspace="5" width="138" height="184">Bookings opened for upscale residences being developed at the Muscat 
            Golf and Country Club, Oman's first golfing green located within 2 
            km of Seeb International Airport. The first scheme of its type in 
            the Sultanate, the development is regarded primarily tourism project 
            although it does offer Omanis the prospect of investing in prime 
            real estate within the country also. Non-GCC nationals are also able 
            to reserve units on condition that a law permitting foreign 
            ownership of land in Oman by expatriates is passed by royal decree 
            before April 2004. <br>
            The promoters of the project HH Sayyid Talal bin Tarik al Said and 
            HH Sayyid Kais bin Tarik al Said are expected to contribute RO2 
            million to the total project cost of RO8 million while RO1 million 
            is expected to come from the Oman and Emirates Fund while it is 
            reported that the remainder will be obtained from government soft 
            loans and commercial financing. <br>
            According to Hamptons International, the agent for the residential 
            units, over 30 per cent of the Club's properties have been sold - 
            the target set by the owners to move ahead with construction of the 
            18-hole PGA golf course. It should be completed within 12 to 15 
            months while the first round of serious golf play will commence 
            around October 2005.</font></p>
            <p align="left"><font face="Verdana" size="1"><b>Chinese launch 
            first manned space flight <br>
            </b>On October 15, China became the third nation to put a person 
            into space. The mission is not any kind of unspectacular 
            technological triumph but it has made clear to the entire world that 
            the communist nation possesses the know-how and financial clout to 
            put a crew into space. <br>
            The Chinese launch of its first manned space flight, the Shenzhou 5, 
            has definitely given the country a place in the elite club of 
            space-faring nations. According to Howard McCurdy, a space policy 
            expert at American University, Washington, the launch proves that 
            China is &quot;a player on the world scene in the modern 21st Century 
            economy.&quot; <br>
            According to analysts, this Chinese mission has not been seen in 
            good light. Until now, the US was the only heavyweight in this field 
            and the country controls about 80 per cent of the world space budget 
            and largely finances the International Space Station. The good old 
            days of Russia's space programme are over and other nations in 
            Europe do have a fleet of planetary probes, but have not committed 
            to manned missions. <br>
            However, the power equation may change with the Chinese manned space 
            flight. According to Phil Deans, director of the Contemporary China 
            Institute at the School of Oriental and African Studies, University 
            of London, &quot;The Americans are very, very concerned. Since the end of 
            the Cold War, they have had almost a complete monopoly on getting 
            themselves into space. Now there's a lot more competition.&quot; </font>
            </p>
            <p align="left"><font face="Verdana" size="1"><b>
            <img border="0" src="mal.jpg" align="right" width="150" height="163">Malaysian PM 
            Mahathir Mohamad steps down </b><br>
            The Malaysian leader Mahathir Mohamad stepped down after 22 years in 
            office - almost half of Malaysia's post-independence life - in which 
            he turned Malaysia into one of Southeast Asia's wealthiest nations. 
            The outgoing premier went out speaking his mind on global political 
            issues. <br>
            Abdullah Badawi has been sworn in as Malaysia's new prime minister 
            and is considered the country's first new generation leader. 
            Abdullah, who was deputy prime minister earlier, has got the 
            privilege of leading a very stable and industrialised nation. The 
            new PM is a clean and honest administrator, but is tough when it 
            comes to the treatment of political opponents and alleged 
            terrorists. The main tasks before him are to gain support within his 
            ruling Umno party and win back lost votes to conservative Islamic 
            opponents.</font></p>
            <p align="left"><font face="Verdana" size="1"><b>Concorde flies its 
            final flight </b><br>
            Three Concordes swooped into Heathrow Airport, joining in a 
            spectacular finale to the era of luxurious supersonic jet travel. 
            The last flight from New York arrived with every seat filled, a feat 
            that had become increasingly rare for a plane that was a 
            technological marvel but a commercial flop. <br>
            Thousands of enthusiasts gathered at the Heathrow airport to watch 
            the landings, but not everyone loved the Concorde. Over the years, 
            many criticised its enormous roar and almost everyone found its 
            fares of $9,000 (RO3,462) or more for a trans-Atlantic round-trip 
            too high. It was a bittersweet end to nearly 28 years of commercial 
            supersonic travel. </font></p>
