              <p><font face="Verdana" size="1"><b>US-led coalition 
              declares war on Iraq <br>
              <img border="0" src="cs-Iraq-war-copy.jpg" align="left" vspace="6" hspace="6" width="250" height="165"></b>Ever so often in the world's journey through time, there comes 
              a moment (or a series of moments) that are defining in some way, 
              that stand out against the milieu of turned-over calendars. 
              Perhaps the most important occurrence in the world economy over 
              the past year was the war with Iraq. <br>
              Before the commencement of the war on March 20, 2003, geopolitical 
              uncertainty and volatility within the global economy were very 
              high. It has since become clear that this uncertainty was a 
              reflection of the unknown impact that a drawn-out conflict with 
              Iraq would cause. The conclusion of the conflict has resulted in 
              the end of that uncertainty and allowed the world economy to 
              become more stable, and rather than focusing on short-run 
              abnormalities and risks, investors have begun to re-focus on 
              underlying economic fundamentals. <br>
              Iraq is undergoing a period of transition since US President 
              George Bush's announcement of the end of 'major combat' against 
              Saddam Hussein's regime on May 1, 2003. The Iraqi economy suffered 
              major disruptions and dislocations because of the devastating 
              effects of the war. Most of the country's political, social, 
              physical and economic infrastructures have, by and large, been 
              destroyed and need to be rebuilt. The US has formed the Office of 
              Reconstruction and Humanitarian Assistance to administer the 
              country on an interim basis, provide humanitarian aid, rebuild 
              infrastructure and help establish a representative government. <br>
              To hazard a guess on the impact of the war on the economy of the 
              region would be imprudent. The regional fallout from war on Iraq 
              can best be understood in the context of its impact on Iraqi's 
              neighbours. For, competing interests across the surrounding 
              countries would vastly affect the rebuilding process of a new 
              Iraq. It is obvious that the war in Iraq disrupted the oil and 
              trade links that had taken shape under the sanctions regime. The 
              Syrian economy depended on the foreign exchange derived from oil 
              exports to Iraq and using smuggled Iraqi oil to meet some domestic 
              consumption. A conservative estimate of loss of revenue resulting 
              from an interruption of oil and trade with Iraq is estimated to be 
              about $2 billion (RO769 million) a year. This would have dire 
              consequences for the Syrian economy. <br>
              Another source of revenue for Syria derives from its involvement 
              in Lebanon, where hundreds of thousands of Syrian workers transfer 
              back home much-needed hard currency. This could be threatened by 
              subsequent challenges to the Syrian role in Lebanon. Jordan, on 
              the other hand, used to receive oil supplies from Iraq at less 
              than half the market price. Cutting these supplies would force the 
              Jordanians to buy energy at commercial prices, which would deepen 
              the crisis of the Jordanian economy. Nonetheless, Jordan obtained 
              free oil supplies from Saudi Arabia, UAE and Kuwait under American 
              pressure to compensate for the loss of Iraqi oil. <br>
              Analysts have pointed out that the post-war future looks quite 
              rosy for Kuwait. Property prices at and near the main border 
              crossing between Kuwait and Iraq have more than doubled and its 
              stock market index has risen by 30 per cent and the PE ratios, 
              literally, went through the roof. <br>
              Last month, the foreign ministers of Syria, Iran, Turkey, Saudi 
              Arabia, Kuwait and Jordan, plus the largest Arab country - 
              politically influential Egypt Ð gathered in a downtown Damascus 
              hotel for talks which Iraqi foreign minister Hoshyar Zebari had 
              rejected invites to attend. After the war, coalition forces 
              established the Coalition Provisional Authority (CPA) as a 
              transitional, interim administration to restore conditions of 
              security and stability in Iraq, create national and local 
              institutions for representative governance in which the Iraqi 
              people can freely determine their own political future, and 
              facilitate economic recovery and sustainable reconstruction and 
              development. <br>
              In July 2003, the CPA established the Governing Council of Iraq as 
              the principal body of the interim administration. With broad 
              membership from across Iraqi society, the Governing Council 
              represents the interests of the Iraqi people and is currently 
              determining the means to establish a representative government to 
              replace the CPA. US government and CPA contracts continue to be 
              the leading business opportunities in Iraq. On September 19, 2003, 
              the governing CPA in Iraq announced sweeping changes to foreign 
              investment laws, permitting foreign investors from any country to 
              wholly own and manage business entities in most Iraqi industry 
              sectors. Under the Hussein regime, all foreign investment was 
              restricted to Arab citizens resident in Arab countries. <br>
              Effective January 1, 2004, Iraq will introduce a personal and 
              corporate income tax limit of 15 per cent. Iraq will also impose a 
              new import Customs duty, called a Reconstruction Levy, of five per 
              cent on most imports. Most economic sanctions against Iraq were 
              lifted by the United Nations and the United States in May 2003.
