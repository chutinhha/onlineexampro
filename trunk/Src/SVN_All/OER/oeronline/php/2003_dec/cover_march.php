            <p align="left"><font face="Verdana" size="1"><b>WHO declares SARS a 
            worldwide threat <br>
            <img border="0" src="sars.jpg" align="right" hspace="5" vspace="3" width="175" height="130"></b>The emergence of Severe Acute Respiratory Syndrome (SARS) during 
            March and April weighed on the global economy. This outbreak 
            negatively impacted the south-east Asian region, with flow-on 
            effects felt throughout the global economy. While it now appears 
            that the incidence of SARS has abated, there may be further 
            implications.<br>
            According to World health Organisation (WHO) figures, around 812 
            people died because of this disease. The main countries to be hit by 
            this infectious disease were China, Hong Kong, Taiwan, Singapore and 
            Canada.<br>
            The SARS virus was first discovered in the southern province of 
            Guangdong in China in mid-November 2002. The Chinese government 
            failed to contain the infection and nearly three months later, 300 
            cases of acute respiratory syndrome were reported in the province.
            <br>
            The disease affected Hong Kong when an infected doctor came to the 
            country from Guangdong, China in mid-February. Thereafter, the virus 
            spread to Canada, Singapore and Vietnam as infected people flew to 
            these countries. <br>
            Travel warning was issued for these countries. Infected people were 
            quarantined, schools were closed, the economy was hit hard as 
            tourist cancelled their reservations. The outbreak had a significant 
            economic impact, especially in East Asia, with some economists 
            warning that it could tip the world into recession. Indeed, the 
            short term impact, while the outbreak continued to spread, was 
            substantial. Tourism, including business travel, retailing and 
            leisure were the main affected industries. <br>
            However, in the end, it did not work out as badly as many feared 
            and, by the end of May, the infection rate dropped sufficiently. 
            Resultantly, WHO lifted its travel warning on Hong Kong and in June 
            dropped China from its list of SARS-infected areas. Of course, there 
            could be a setback if SARS returns this winter in the northern 
            hemisphere, as some health experts say it might.</font></p>
