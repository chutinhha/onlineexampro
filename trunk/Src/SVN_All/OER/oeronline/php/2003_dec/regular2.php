            <br>
            <b>2003: The Year That Was</b><br>
            Year 2003 has seen the MSM Index rise a significant 40 per cent for 
            the 10 months' period ended October 2003. Liquidity in the stock 
            market has been at healthy levels - average daily trading volume on 
            MSM has hovered around RO1.5 – RO2.0 million for the current year. 
            Following the good recovery last year when the MSM Index gained 25 
            per cent, the market sentiment has stayed positive all through the 
            current year. Stocks across the listed market sectors like 
            financial, manufacturing and services have recorded substantial 
            gains. A major factor explaining the attractive returns in Omani 
            equities this year is the strong impetus to economic growth across 
            the GCC countries from buoyancy in the international crude oil 
            prices. Average crude oil price for the current year is around 
            $27-29 per barrel ($22-24 per barrel last year). Growth in budgetary 
            spending by GCC governments this year has helped sustain a 
            reasonable growth in non-oil sectors of these economies. The healthy 
            environment for local businesses, especially in the private sector, 
            has triggered a mood of optimism among investors across the GCC 
            stock markets.<br>
            Earnings growth supported by an increase in stock market liquidity 
            has re-rated stock valuations on the upside. Aggregate earnings 
            growth posted by listed companies in Saudi and Kuwaiti stock markets 
            are 70 and 40 per cent respectively – in Oman, the growth posted is 
            20 per cent. Stock valuation indicators denote over-heated stock 
            markets – P/E multiple for Kuwaiti stock market has been revalued 
            upwards and is currently at 18 times; 22 times for the Saudi stock 
            market (these valuations were around 12-14 times last year). For 
            Oman, the P/E multiple has expanded from 8-10 times last year to 
            12-14 times in October 2003. The benchmark stock market indices in 
            Kuwait and Saudi Arabia, the two most dominant GCC stock markets, 
            are up 80 and 60 per cent respectively. The success of the coalition 
            forces led by US in April this year in taking control of the 
            political administration in Iraq has brought in a stability factor 
            for Iraqi economy. With security risks concerning Iraq and Kuwait 
            being resolved now, it has given one more reason for investors to 
            feel optimistic about growth prospects for GCC economies in general 
            and Kuwait in particular. <br>
            Reflecting this optimism, Omani equities, which tend to draw its 
            valuations from other GCC markets, have seen a substantial rise in 
            valuations this year. Except for NBO, which has been hit with 
            substantial loan-loss provision charges and consequently huge 
            losses, other banks have performed reasonably well this year. 
            However, its negative earnings performance this year has been 
            overshadowed with expectations of a significant turnaround in 
            earnings next year and also the fact that the Suhail Bahwan Group, a 
            respected business house in Oman, acquired a substantial 35 per cent 
            equity stake in the bank in October 2003. The bank is well 
            capitalised now and likely to post considerable earnings improvement 
            next two years. This has led to good levels of buying interest in 
            the NBO stock, with the result that the NBO share has recorded a 
            significant move from around RO 1.000 early in the year to its 
            current price of around RO 2.600. It is among a key group of stocks, 
            which have the highest trading liquidity on MSM and also have a 
            substantial bearing on the direction of the Omani stock market. <br>
            Major stocks in this group include OIB, BankMuscat, Ominvest, DIDIHC, 
            Oman Cement, Topaz Energy &amp; Marine, Renaissance Services and OIFC. 
            Stocks of companies engaged in financial services like Financial 
            Services, Global Financial Investments, Gulf Investment Services and 
            United Finance has also seen increased trading activity in the 
            current year on the support of healthy investment gains reported by 
            these investment companies. <br>
            Among the major market developments this year were the acquisition 
            of Majan Bank by Bank Dhofar in March this year followed by the 
            first merger among brokerage companies in May wherein three 
            companies – Al Mawarid Securities, Al Shalman Securities and 
            Investment Banking Division of Oman &amp; Emirates Investment Holding 
            Company merged to form a newly incorporated entity, The Financial 
            Corporation SAOG (Fincorp). <br>
            In July, the government divested around 10 per cent of its stake in 
            the two leading industrial companies controlled by the government – 
            Oman Flour Mills and Oman Cement Company. The public sale of these 
            shares was oversubscribed. By end of the year, the government is 
            expected to divest its 65 per cent equity stake in Al Maha Petroleum 
            Products Marketing Company – the third company engaged in retail and 
            bulk sales of petroleum products in Oman after Shell Oman Marketing 
            Company and Oman Oil Marketing Company (erstwhile BP Oman SAOG). <br>
            The much-awaited Initial Public Offering (IPO) of Oman 
            Telecommunications Company SAOC (Omantel) is now most likely to be 
            floated to the public next year. The listing of Omantel will improve 
            the profile of MSM and also help in generating higher levels of 
            trading liquidity on MSM. In corporate developments, the successful 
            turnaround of OIFC early in the year has drawn a surge in market 
            interest for the company’s stock. Post-restructuring of its bad 
            debts and a few profitability enhancing measures, the company now 
            bears potential to report healthy earnings for FY04 and, hence, the 
            jump in its stock price this year from RO 1.000 to current levels of 
            around RO 3.000. <br>
