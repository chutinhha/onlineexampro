            with the overall sector gaining by 2.5 per cent. However, the 
            volumes were topped by the Banking sector with National Bank of Oman 
            maintaining its lead after the announcement of the strategic stake 
            to be taken by the Suhail Bahwan group. Even though the share price 
            firmed up from RO2.630 in the initial days, beginning from the 
            mid-month period of October to top at RO2.800, the stock came under 
            intense selling pressure from profit bookers in spite of absorbing 
            large volumes during the period. Overall, close to 20 per cent of 
            its total equity shares were traded during the month. <br>
            Majan College and Oman Holding International continued their upward 
            momentum, gaining handsomely by 34 and 30 per cent respectively 
            during the period, albeit on low volumes on announcement of better 
            financial performance. However, the star performer for the month, 
            based on both value and volume had been Oman and Emirates Investment 
            Holding Co. which, after breaking its initial barrier of 330 baiza, 
            gained 30 per cent to close at 440 baiza on large volumes during the 
            initial part of the month. <br>
            The losers too were largely from the Industry sector even though the 
            overall sector gained. These were basically the illiquid stocks like 
            Oman flour Mills, Al Khaleej, United Power and Oman Chlorine. The 
            reasons varied from profit booking to a lower than expected 
            performance in the latest quarter ending September 2003 or post-tax 
            dividend prices.<br>
