            convertibles, whose price peaked to RO1.030 before stabilising at 
            RO1.020 for the period under review. With the conversion of the 
            BankMuscat convertibles into BankMuscat shares, taken on the 
            weighted average price for the last three months ending December 
            2003, the stock would be the most noticeable in the bonds markets. 
            Other BankMuscat bonds - both guaranteed and non-guaranteed remained 
            fairly liquid but remained in a narrow trading zone.<br>
            The overall market remained dull with a drop in average volumes of 
            traded shares during the fortnight ending November 13, 2003 on 
