            <font size="4" color="#FF0000" face="Verdana">On the rebound? </font>
            <font face="Verdana"><font size="4" color="#FF0000"><br>
            <br>
            </font>
            </font><font face="Verdana" size="1"><b>These are testing times for 
            PDO. John Malcolm, managing director of PDO, recently discussed the 
            company's performance in 2003 and how the company will have to focus 
            on enhanced oil recovery techniques and cost cutting during the next 
            five years. Nayl D'Souza reports </b></font></p>
                    <p class="MsoPlainText"><font face="Verdana" size="1">
                    <img border="0" src="pdo.jpg" align="left" hspace="5" vspace="3" width="175" height="181">Petroleum Development Oman (PDO) is striving to turn around 
                    its lagging oil production to regain its leading position in 
                    the region. Shortly after taking over as managing director 
                    of the state-led company last year, John Malcolm declared at 
                    a press briefing that PDO had reached a defining moment. He 
                    told OER: &quot;We probably will not see many significant 
                    benefits from the changes in management systems and 
                    strategies implemented to reverse the trend of declining 
                    production until 2004 at the earliest.&quot; He agreed, however, 
                    that the level of progress the company had gained in 2003 
                    would be a useful litmus test to ascertain whether PDO was 
                    on course to sail through its &quot;rough patch.&quot;<br>
                    <br>
                    Last month, Malcolm hosted another press event to explain 
                    how PDO was aiming to skillfully exploit developmental 
                    &quot;waves&quot; to help Oman's oil production return to higher 
                    levels. He confirmed: &quot;The year 2003 has been a good one for 
                    PDO. We are intently focusing on the underlying issues to 
                    deliver on our promises and plans for the future.&quot; <br>
                    <br>
                    The company's goal is to achieve more than 800,000 barrels 
                    of oil per day by 2008--production levels reminiscent of 
                    those it had achieved in the late 1990s. News that PDO 
                    intends to invest a massive $2 billion during the next five 
                    years on enhanced oil recovery (EOR) is sure to raise 
                    expectations that the company can indeed blaze a trail 
                    towards this goal. <br>
                    <br>
                    &quot;We are going to invest a large amount of money into 
                    something that will have a payback in several years' time. 
                    But when EOR does yield a payback, it will produce oil year 
                    after year after year and should unlock hundreds of 
                    millions, if not billions, of barrels of reserves for the 
                    country.&quot; <br>
                    <br>
                    Another focus of PDO's development programme between 2004 to 
                    2008, is the company's planned $2 billion reduction in 
                    capital expenditure (CAPEX) and operating expenditure (OPEX). 
                    &quot;If we sort out the cost, the other aspects of production 
                    will come with it,&quot; he added. </font></p>
            <p class="MsoPlainText"><b><font face="Verdana" size="1">How PDO 
            fared in 2003<br>
            <br>
            </font></b><font face="Verdana" size="1">Notwithstanding its 
            challenging plans for future years, the company is happy to report 
            that this year it has delivered what it promised. At present, PDO is 
            likely to achieve an average production of 703,000 barrels of oil a 
            day for 2003. That's within 1 per cent of its annual production 
            target. Moreover, the company looks likely to be within 1 per cent 
            of its capital expenditure budget for the year. To be so close to 
            its production forecast and to have nearly spent all its capital is 
            certainly good news.<br>
            <br>
            We shouldn't forget that PDO has also proven its worth in Oman's gas 
            industry. About 30 per cent of the hydrocarbon energy that the 
            company currently supplies comes from natural gas--the fuel that has 
            a central role in Oman's economic diversification plans. This year, 
            like every year since 1978, when PDO first started producing gas for 
            the government's gas system, it will have delivered an uninterrupted 
            supply of gas to all of its customers. <br>
            <br>
            Away from these headline stories are the achievements in well and 
            reservoir management, waterflooding, EOR, exploration, and other 
            support factors that have been key enablers to get PDO back on 
            track. </font></p>
            <p class="MsoPlainText"><b><font face="Verdana" size="1">Well 
            managed<br>
            </font></b><font face="Verdana" size="1">The optimisation of the 
            development of existing fields has been a priority, so the setting 
            up of the Study Centre, which is dedicated to looking at PDO's 
            medium- and long-term production needs, is a welcome step. In 2004, 
            the company plans to carry out something like 162 man-years of 
            petroleum-engineering studies from the new research facility.<br>
            <br>
            &quot;It is a really tremendous achievement to have got the Study Centre 
            up and running so quickly. It is easy to be sidetracked into 
            focusing on short-term production needs while overlooking the need 
            to plan properly for the future. We are now in a better position 
            thanks to the Centre&quot;, says Malcolm.<br>
            <br>
            Another improvement in PDO's well and reservoir management has 
            resulted from the grouping of reservoirs of similar type. Instead of 
            looking at each of more than one hundred reservoirs in isolation, 
            the company has now clustered them both for analysis and 
            development. Cluster teams have monthly targets to meet, and greater 
            care is being taken to assign staff members to the clusters that can 
            benefit most from their expertise.<br>
            <br>
            <b>Well watered<br>
            </b>Waterflooding, which involves injecting water into reservoirs to 
            sweep the oil to producing wells, has progressed significantly in 
            Oman this year. Currently, PDO has three major waterflood projects: 
            one in Lekhwair, another in Marmul, and the third in the Dhulaima 
            and Fahud area. A fourth waterflood project in the Al Burj cluster 
            in the south of Oman may make an important contribution from 2005 to 
            PDO's production. All of these projects are currently on track.<br>
            <br>
            <b>Well enhanced<br>
            </b>Enhanced oil recovery (EOR) techniques have been under 
            consideration at PDO for a long time, since they offer many keys to 
            the doors of Oman's untapped reserves. EOR is a case of &quot;finding the 
            best key to unlock more reserves.&quot;<br>
            <br>
            Two different EOR technologies are being implemented by PDO. The 
            first is miscible gas injection: a method of making oil flow more 
            easily to wells by dissolving gas in it. Late next year, the company 
            expects the Harweel cluster of fields in south Oman to come on 
            stream in a fairly conventional way, producing something like 10,000 
            barrels a day. But after the full-scale implementation of the 
            miscible-gas technique, the field cluster's production rate is 
            expected to be 10 times higher.<br>
            <br>
            The second EOR technology that PDO is implementing is based on steam 
            injection: a method of making oil flow more easily to wells by 
            heating it with steam. The potential acceleration of the Mukhaizna 
            development is presently being reviewed, while Qarn Alam, a mature 
            steam-injection pilot, is expected to reach a production target of 
            around 16,000 barrels a day by 2005.<br>
            <br>
            Says Malcolm: &quot;Harweel--if we manage all the difficulties facing the 
            project and operations teams--will be delivering 100,000 barrels a 
            day within five years. In Mukhaizna, too, our aim is to really 
            accelerate the development very quickly indeed and to make volumes 
            in the order of 50,000 barrels a day in a similar timescale.&quot; <br>
            <br>
            Before 2010, PDO expects to produce 250,000 barrels a day of EOR 
            oil. If so, it will become one of the leading EOR companies in the 
            world. <br>
            <br>
            <b>Well explored<br>
            </b>Turning to oil exploration, there has been a greater focus on 
            looking for carbonate stringers, the salt-encased carbonate rock 
            formations that are found in south Oman. (The Harweel field is one 
            such formation.) In addition, PDO continues to explore for gas. It 
            would also appear that the company's new spirit of 
            inter-departmental co-operation is enabling oil wells come on stream 
            quicker. The company plans to produce oil from the Dafaq field, 
            which was discovered in 2002, as early as next year. &quot;That's a 
            tremendous achievement, given that in last year's plan Dafaq was 
            meant to come on stream only in 2008,&quot; Malcolm remarks.</font></p>
            <p class="MsoPlainText"><b><font face="Verdana" size="1">Well 
            supported<br>
            </font></b><font face="Verdana" size="1">PDO has also scored success 
            on some support issues. On-line bidding, which facilitates about 25 
            per cent of PDO's total expenditure, has now become key to the 
            company. &quot;Not only does it improve the cost performance of PDO, it 
            brings in more people to bid and it makes the process faster and 
            more efficient. PDO, as a leader in digital commerce, is spreading 
            those advantages around the whole business community in Oman.&quot;<br>
            <br>
            &quot;Another core support area that deserves mention is logistics. As 
            part of a joint initiative with well engineering (including 
            contractors), our logistics people have achieved tremendous 
            improvements in the speed with which we move our drilling rigs.&quot; <br>
            <br>
            <b>Future plans<br>
            </b>So it's plain to see that PDO has taken many steps in 2003 
            towards recovery. Of course, PDO plans to continue optimising 
            production from existing wells and harvesting the fruit of previous 
            years' capital expenditure. It will also plan more infill drilling Ð 
            drilling new wells in and around existing ones. This process 
            contributes a massive amount to the company's production every year. 
            For example, a back-to-basics drilling campaign which started in 
            January 2003 in the Lekhwair cluster has been hailed as a great 
            success. It slashed costs and drilling times while exceeding 
            production expectations. <br>
            <br>
            It's still too early to confirm that PDO is 'on the up' just yet. 
            But the company's management is confident that it can move Oman's 
            oil industry forward prolifically. There are, however, still many 
            challenges to overcome. And production is only half the story. The 
            company plans to fundamentally change the way it does its business 
            to control its costs. By driving costs down and increasing 
            efficiency in 2004, PDO expects to make savings worth $150 million, 
            which is equivalent to 7.5 per cent of next year's budget--a good 
            start to the $2 billion programme of cost reductions PDO envisages 
            for the next five years. <br>
            <br>
            The bulk of PDO's money--nearly half of its capital expenditure--is 
            spent on well engineering. Oman's oil-services community may 
            therefore have grounds to worry about what may happen to their 
            profitability. Yet Malcolm is adamant that cutting expenditure will 
            not simply be a matter of reducing the margins of PDO's contractors. 
            &quot;We have been engaging the well-engineering community to come up 
            with proposals to help us to work better in partnership with them to 
            reduce our costs&quot;, he says. Moreover, PDO's management team has 
            compiled a detailed cost register that will outline how the company 
            is going to manage all its expenditure starting in 2004.<br>
            <br>
            If successful in optimising production and controlling costs within 
            the next five years, PDO believes it will be acknowledged as one of 
            the best land-based exploration and production companies in the 
            world. In doing so, it will be producing the much-needed oil and gas 
            that fuels Oman's economic development. The mood in PDO is positive, 
            but the stakes couldn't be higher and the clock is ticking.</font></p>
