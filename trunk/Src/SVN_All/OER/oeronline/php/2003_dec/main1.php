            </font><b><font face="Verdana" size="1">With Oman's depleting oil 
            wealth, the role of the manufacturing units and industries assumes 
            greater importance in sustaining the country's economy. Dr Hamad bin 
            Hashim Al-Dhahab, Director-General of Industry, Ministry of Commerce 
            &amp; Industry, provides an overview of the Omani manufacturing sector 
