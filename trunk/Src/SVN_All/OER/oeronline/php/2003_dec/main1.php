            <p><font face="Verdana" size="1"><b>
            <img border="0" src="manu1.jpg" align="left" hspace="6" vspace="6" width="197" height="220">Has 
            the local industry gained the kind of expertise to live up to those 
            expectations?</b> Before I answer that question, let's first get a 
            brief background of the manufacturing sector in Oman since we will 
            be doing it injustice if we compare it with the manufacturing 
            sectors of service economies. Oman's industrial sector started in 
            1975 - so it is very young - and the immediate target set for it was 
            that of import substitution. <br>
            It started off in small measures, and even today, most of the 
            products are consumer products and not engineering goods. Right from 
            that time onwards, the government has helped it get a foothold by 
            providing infrastructure like electricity and means of 
            communications etc. That is when the Industrial Estate Authority was 
            formulated in an attempt to attract the private sector. The Rusayl 
            Industrial Estate was the first one to come up and, today, there are 
            six industrial estates throughout the Sultanate. Thus, the 
            government has played the role of a catalyst for the private sector 
            to invest in the manufacturing industry. <br>
            Now, the changing of a society from being trading-based to becoming 
            industry-based takes time because it is a learning process. And so 
            was the case with Oman, when third-generation traders invested in 
            industry, which started catering to local consumers. From that 
            point, the industry has come a long way and, in 1996, according to 
            Vision 2020, a certain target was set for it. Of course that target 
            has been broken up into five-year plans and, thus, while the horizon 
            is longer, planning has been steady. <br>
            The manufacturing sector, which was contributing 4.6 per cent of GDP 
            in 1996, has been set a target of 15 per cent of GDP in 25 years 
            from now. Industry focus too has graduated from import substitution 
            to diversification. Diversification to a non-oil based economy is a 
            national priority and the country has clear targets for this. The 
            manufacturing sector is a key area for achieving this. Of course, 
            tourism is equally important. The local industry has gained 
            expertise to remain competitive at the regional level and some even 
            at a global level.<br>
            The way forward in that direction is the setting of what we call the 
            strategic pillars of industry. The focus now is on hi-tech, capital 
            intensive industries that mainly use local raw materials besides 
            being export-oriented and, of course, those that provide consumers 
            with more value-addition. <br>
            <br>
            <b>
            <img border="0" src="manu2.jpg" align="right" hspace="6" vspace="6" width="250" height="157">Of 
            all the non-oil/gas industrial sectors, which one do you think is 
            the most promising?</b> <b>How can the government ensure its future 
            growth and sustainability?<br>
            </b>A study being conducted by one of the government agencies has 
            identified about 20 thrust sectors, which contributed around 60 per 
            cent to the exports of non-oil Omani manufactured exports of RO265 
            million in 2001. Most of the companies in the above sectors are 
            operating at their full capacity, which is an indication of their 
            success. However, these are interim results and we may have to wait 
            for the final conclusions. <br>
            The real challenge, however, comes from the fact that many economies 
            in the world are transforming to knowledge-based ones and ICT 
            (information communications technology) is becoming the engine of 
            growth. Oman has to catch up with this fast-track trend, which is 
            not easy. Anything from education to governmental systems and 
            procedures has to be tackled from a holistic angle - one cannot have 
            a narrow developmental approach. <br>
            We have already initiated steps such as KOM (Knowledge Oasis 
            Muscat). Some legal and financial incentives related issues are 
            being evolved. The recently held GCC Conference on IT was a good 
            shot in the arm. In fact, the current e-governance initiatives in 
            Oman as well as in the region would open up many avenues for IT 
            business. Therefore, I am tempted to say that the IT sector looks 
            quite promising. <br>
            <br>
            <b>Why is there such a high number of sick industrial units in Oman, 
            and what is the Ministry doing in this regard - firstly to turn 
            around the sick units and secondly to avoid other units falling sick 
            in the first place? <br>
            </b>Industrial sickness is a phenomenon found in any economy. 
            Sometime back, the Ministry of Commerce &amp; Industry had conducted a 
            detailed study of 168 industrial units. Forty-eight per cent of the 
            sample was found to be healthy and 20 per cent on a low performance 
            level. If left unattended, this 20 per cent would be vulnerable to 
            industrial sickness.<br>
            Thirty-two per cent of the sample was actually sick. However, the 
            study was conducted during a period of economic downturn and this 
            would have influenced the results. Our analysis also revealed that 
            many of the reasons for sickness were management related.<br>
            Having understood the magnitude of the problem, rehabilitation of 
            sick units has been given substantial importance. We now have a sick 
            unit rehabilitation cell. We are sharing the findings of our study 
            with those establishments which are in trouble. It will be wise on 
            their part to make use of this valuable information and take 
            corrective actions. The Ministry, since about a year, is 
            facilitating to bring together all the stakeholders - promoters, 
            banks and the government - to evolve revival plans.<br>
            All the companies identified as having problems have been considered 
            for rehabilitation. Rehabilitation plans have been finalised for 21 
            units and are at various stages of restructuring; seven are under 
            study now. <br>
            <br>
            <b>What are the other major problems plaguing the Sultanate's 
            industrial sector and what is being done to overcome them? </b><br>
            It is too well known that the home market is pretty small. Hence, 
            without export, it was rather impossible to achieve economic scale 
            of production. In the earlier years of industrialisation of Oman, we 
            had a lack of enough experience at the entrepreneurial or managerial 
            level for playing in the export market, especially by operating from 
            a land with very limited comparative advantages in terms of key 
            industrial resources. But, as of now, development is not really 
            dependant on resource endowment. It largely depends on the 
            developmental strategies pursued by nations. To this extent, I 
            think, Oman is geared to face the current challenges. <br>
            As of now, as part of a global economy, Omani companies also face 
            severe competition in export as well as local markets. It is quite 
            possible that our industries are not quite certain about their 
            competitiveness in their respective sectors, as it is rather 
            difficult to assume that we have low cost of production in Oman 
            compared to other GCC countries or other parts of the world. We need 
            to have some kind of benchmarking in certain key sectors within 
            Manufacturing. We might embark upon some exercise in this direction. 
            But we have already initiated sectoral studies in the manufacturing 
            sector. <br>
            We have finished a detailed sectoral study on the plastic industry 
            in Oman by which we have studied the industries in Oman in relation 
            to the regional and to some extent the global performance. As a 
            result of this, we have identified two issues to be addressed in 
            this sector immediately. One, to initiate a programme called 
            'Quality Omanisation', which is aimed at enhancing the quality and 
            productivity of Omani employment, wherein both skill and attitudinal 
            issues will be addressed simultaneously through a structured HRD 
            programme. In this regard, we might seek the help of a well-known 
            plastic research and training institution. The second issue is to 
            explore the possibility of micro-enterprises in this sector in Oman.<br>
            Another problem facing industry is the unfair practices in global 
            trade carried out by exporting countries and, of course, issues 
            related to dumping. Although Oman initiated the process of evolving 
            anti-dumping laws, in the current context of a single customs union 
            within the GCC, probably this would be evolved at a regional level.
            <br>
            Productivity concepts, I suppose, still take a back seat when it 
            comes to industrial performance. We need to have continuous 
            productivity enhancement programmes in various industrial sectors.
            <br>
            <br>
            <b>What steps is the Ministry taking to strengthen the country's 
            industry in terms of providing incentives and tax-holidays to new 
            and budding units? </b><br>
            a. Government soft loans with an interest rate of 3% for investment 
            projects. <br>
            b. Plant, machinery and spare parts imported for industrial 
            production are exempted from Customs duties. Raw and semi-processed 
            materials imported for manufacturing and which are not available 
            locally are exempted from paying Customs duties. <br>
            c. Oman's investment laws guarantee exemption from income tax for a 
            5-year period, which may be renewed for another period of 5 years. 
            Oman does not levy any personal income tax. <br>
            d. Oman has a liberal Foreign Capital Investment Law. This law has 
            been further liberalised when the government decided to permit 
            foreign equity up to 70 per cent. Appropriate legislative amendment 
            is being made. Even 100 per cent foreign equity could be permitted 
            by the government in case a project is considered vital for 
            development of national economy. <br>
            e. Planned and serviced plots are provided by the government to set 
            up industrial units. Ready-built factories of various sizes, which 
            can be leased for a period of 25 years (renewable) are also 
            available. Services provided at the industrial estates include 
            roads, water, gas, means of communications, liquid waste processing, 
            solid waste collection and disposal, etc. The government levies only 
            a nominal rent on the premises at the industrial estates as given 
            below: <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
            Annual rent for land plots: 250 baisas/ sq m for the first 5 years<br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
            500 baisas/sq m after 5 years <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
            Annual rent for building: RO2-4/sq m<br>
            f. Foreign investment projects are exempt form tax on profits for a 
            period of 5 years, renewable for another 5 years. (Recent tax reform 
            has unified the income tax rate to 12 per cent, for all companies 
            registered in Oman, irrespective of the extent of foreign 
            ownership.) <br>
            g. KOM will provide not only state-of-the-art infrastructure to ICT 
            firms, but also incentives such as allowing 100 per cent foreign 
            ownership, competitive telecom tariffs etc. This is apart from the 
            subsidised incubator facilities that will be made available to IT 
            start-ups and first-generation entrepreneurs. <br>
            h. In order to give a differential treatment to the ICT sector, we 
            have almost evolved funding norms for supporting the IT industry. 
            Creation of a Venture Capital Fund, seed capital support, support 
            for R&amp;D etc. are all being thought of and being discussed at various 
            levels. <br>
            <br>
            <b>Are the local manufacturing units on track to meet the target of 
            the current five-year development plan? </b><br>
            The Sixth Five-Year Plan envisages an average growth of 5.2 per cent 
            in the non-oil activities. The first year of the sixth plan period 
            has yielded a growth rate of 55 per cent in the manufacturing sector 
            as a whole, largely on account of the LNG project. However, the 
            other (non-oil) manufacturing sector alone has a consistent growth 
            of 5.2 per cent during the periods 1999-2000 and 2000-01.<br>
            In 2002, there was a negative growth (-5.8 per cent) in GDP on 
            account of the 'whole manufacturing sector', which includes 
            petroleum and gas-based activities. If we exclude petroleum and gas, 
            the growth rate in GDP on account of 'rest of manufacturing' was 3.1 
            per cent. In fact, the negative growth in the 'whole manufacturing 
            sector' was on account of the reduction in gas-based activities.<br>
            As against the Vision 2020 target of a 15 per cent share in GDP, the 
            entire manufacturing sector contributed 5.4 per cent in the year 
            2000, 8.3 per cent in 2001, and 7.7 per cent in 2002. Several 
            projects that were planned in the Fifth Five-Year Plan are likely to 
            be implemented during the Sixth Plan period (2001-2005). The IT 
            Park, which is being created at Rusayl, would not only attract IT 
            investment but also boost the IT business in the country. Therefore, 
            the overall picture is quite positive. <br>
            <br>
            <b>What help is available to a young entrepreneur with little 
            personal finance to start a factory of his own in Oman? What role 
            does your department play in this regard? </b><br>
            The Oman Development Bank was established through a Royal Decree in 
            1997 with the objective of advancing finance to small- and 
            medium-sized projects. Ministry of Commerce &amp; Industry has grant 
            schemes in operation to support small enterprises. At the same time, 
            programmes such as the Sanad would be helpful in nurturing micro 
            enterprises. <br>
            The incubators at KOM will nurture many first-generation IT 
            entrepreneurs. They would be getting a kind of all-under-one-roof 
            support from KOM - from project counselling to running the business. 
            As mentioned earlier, in case of IT projects, we are working on 
            evolving special norms to support young entrepreneurs. Creation of a 
            Venture Capital Fund is also being considered. <br>
            Though not government, the Youth Fund and Intilaaqah are doing a 
            good job in assisting young entrepreneurs. The Youth Fund encourages 
            Omani youth to establish small and medium enterprises, wherein the 
            Fund authorities will assist the entrepreneur from concept to 
            commissioning and also provide managerial guidance apart from 
            directly participating in financing the equity of the project. <br>
            Intilaaqah, on the other hand, works with the objective of 
            stimulating and encouraging young Omanis to consider the option of 
            starting their own businesses and to provide assistance to those who 
            wish to take up that option. Since its inception in 1995, 
            Intillaaqah has so far trained more than 300 aspirants out of which 
            more than 70 have started their own businesses.</font></p>
