                </font><b><font face="Verdana" size="4">Pathbreaking performance<br>
            </font>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">Al Amal 
            Fund has adopted a combination of prudent investing principles and a 
            scientific research based approach to give its investors “above 
            market returns.” Mayank Singh reports</font></b></p>
            <p align="center" style="margin-left: 10px; margin-right: 10px" dir="ltr"> <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><img src="images/finance01.jpg" width="450" height="299"></font></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> <b> 
                The numbers speak for themselves </b>– Al Amal Fund managed by 
                FINCORP has achieved a year-to-date return of 21.11per cent (as 
                on June 16, 2009) compared to a 4.6 per cent return given by the 
                MSM 30 index from the beginning of the year. On an annualised 
                basis this works out to be a 36 per cent higher return than the 
                index. Taking the equation further – the fund has given a total 
                return of 55.29 per cent since its inception on March 20, 2005; 
                far in excess of the 32.14 per cent MSM 30 return over the same 
                period. Says HE Munir Makki, managing director and president, 
                FINCORP, “Markets are extremely volatile and identifying attractively 
                valued growth stocks needs a deep understanding of companies, 
                industry and market experience. Equity is an important asset class 
                and needs to form an integral part of each investor’s asset allocation. 
                However retail investors should avoid direct exposure to the equity 
                markets in view of their complex nature and instead should channelise 
                their investments through mutual funds (MFs) with a good track 
                record like Al Amal as this minimises the risk of capital erosion.” 
                Al Amal currently has a fund size of RO5mn out of which FINCORP 
                has contributed 35 per cent. <br>
                <br>
                <b>Re-inventing the wheel<br>
                </b>The fund has emerged a winner by doing things differently. 
                At a time when most MFs in the market were close ended, Al Amal 
                was floated as an open-ended fund. An open-ended fund is a collective 
                investment scheme which can issue and redeem shares at any time. 
                Compared to this a closed-ended fund is a collective investment 
                scheme with a limited number of shares. New shares are rarely 
                issued after the fund is launched and shares are not normally 
                redeemable for cash or securities until the fund is liquidated. 
                The investment approach adopted by the fund manager has been another 
                reason for Al Amal’s success. The fund manager has consistently 
                adopted a prudent approach of booking profits and remaining in 
                cash as the index moved upwards. <br>
                <br>
                The global financial crisis and the crash in oil prices from $147 
                per barrel in July 2008 to below $35 per barrel in December 2008, 
                took its toll on the MSM. When the MSM 30, hit 4,188 levels in 
                January 2009, the fund manager used the higher liquidity to re-enter 
                the market – convinced about the inherent strength of the economy.<br>
                <br>
                <b>A good foundation<br>
                </b>Says HE Makki, “The economic crisis has had a lower impact 
                on Oman. A few banks may have some issues due to the economic 
                meltdown, but that’s it. There has been no drop in the budget 
                or in government spending and oil prices are currently much higher 
                than the budgeted price of $45 per barrel. The selloff in the 
                market came mainly from foreign institutional investors and not 
                from Omani investors.” The panic in the wake of the global meltdown 
                saw close to 16 companies (a number of them being fundamentally 
                strong) trading below their respective book values.<br>
                <br>
                Having burnt their fingers in the meltdown, a number of MFs opted 
                for defensive, high dividend yielding stocks like oil marketing 
                and power companies. Taking a contrarian approach, Al Amal went 
                in for growth stocks like Galfar Engineering and Renaissance Services. 
                Says HE Makki, “One should go into defensive stocks when the market 
                is trading high. We changed from defensive stocks to growth stocks 
                as they would go up much faster after a severe downturn.” As the 
                markets went up from 4,628 on March 31, 2009 to 5,687 levels on 
                June 16, 2009; this move paid off in good measure.<br>
                <br>
                <b>Mutual fund option <br>
                </b>The success of Al Amal Fund shows the scope of MFs as an investment 
                option for investors. Investing intelligently in the markets calls 
                for a sophisticated knowledge of the stock market and most small 
                investors may not be equipped with such insights. A good fund 
                gives a small investor the benefit of using experienced resources 
                backed by research for value based investing. Says HE Makki, “We 
                adopt a top down approach to arrive at stocks to invest in. People 
                at times do tend to get sentimental about stocks which they should 
                not.”<br>
                <br>
                Companies that have performed very well over a long time are usually 
                better placed to weather a downturn as they have a good management, 
                a sustainable model and a competitive business. However investors 
                need to keep abreast of the trends like the evolution of the technology 
                era in the 21st century as compared to the industrial revolution 
                in the previous century. HE Makki cites an example – “In the US, 
                industrial companies were the biggest players on the US stock 
                exchanges in the 1970s and 80s, but today that space has been 
                occupied by tech companies like Yahoo, Microsoft, Google and E-bay.” 
                Thus an investor has to ascertain these changes while making an 
                investment decision. <br>
                <br>
                Despite their advantages, MFs have still not caught the fancy 
                of a number of small investors in the Sultanate. There is a strong 
                need to educate investors about the positive aspects of investing 
                in funds as against direct investment in the stock markets. Says 
                HE Makki, “The Capital Market Authority (CMA) and the government 
                need to encourage small investors by appropriate publicity and 
                educational campaigns on the merits of investing through MFs. 
                The CMA could reserve a percentage of initial public offers to 
                MFs with a view to encourage MFs as an important asset class. 
                MFs could play a major and significant role in the development 
                of the capital market. Investors need to look at MFs as a medium 
                to long term investment asset class with a 18 to 24 month investment 
                horizon.”<br>
                <br>
                Al Amal has issued bonus shares and 10 per cent dividend to its 
                investors since inception. Starting at a par value of one rial 
                in March 2005, the fund had a unit value of RO1.440 as on June 
                16, 2009. Al Amal’s management fee stands at 1.7 per cent.<br>
                <br>
                FINCORP is a leading investment bank and provides innovative, 
                structured and customised solutions to its clients. The company’s 
                product offerings include asset management and discretionary portfolio 
                management, corporate finance and advisory, brokerage and corporate 
                research services.</font><br>
