                    </font><font face="Verdana" size="1"><br>
                    On the 1st of June, this year, the automotive industry was 
                    hit by a huge bombshell. It was on this date that General 
                    Motors Corporation, or GM as most of us know it, filed for 
                    government-assisted chapter 11 bankruptcy protection. This 
                    filing was the fourth largest of its kind in US history, following 
                    Lehman Brothers Holdings Inc, Washington Mutual and WorldCom 
                    Inc. First and foremost we have to realise that Chapter 11 
                    is not truly bankruptcy, it is bankruptcy protection. What 
                    this means is, GM is not currently in a position to payback 
                    all its stakeholders. To prevent itself from going bankrupt 
                    and being liquidated, GM filed for the Chapter 11. In doing 
                    so the company can, with the help of the court, get restructured. 
                    This process will allow the launch of a &lsquo;New GM&rsquo;, 
                    which will purchase all the productive assets of the old company. 
                    As such its payments to employees, warranty coverage, vehicle 
                    service and other current operations remain relatively unaffected. 
                    In fact, it is possible that the New GM may emerge a leaner 
                    company with a significantly stronger balance sheet, a world 
                    class product line-up, a global footprint and industry leading 
                    expertise and technology.</font><font face="Verdana" size="1"><br>
                    <br>
                    <strong>Worst nightmare</strong><br>
                    Also in lieu of the filing, in fact even prior to it, GM announced 
                    the phase-out of its Pontiac brand by late 2010 and that it 
                    would concentrate on its four primary brands - Chevrolet, 
                    Cadillac, Buick and GMC. The remaining three - Hummer, Saturn 
                    and Saab were to be sold. And this is just what was done starting 
                    with the announcement on June 1 that Hummer was to be sold 
                    to China-based Sichaun Tengzhong Heavy Industrial Machinery 
                    Company, on June 5 it was announced that Saturn would be sold 
                    to Penske Automotive Group and finally on June 16 it was announced 
                    that Koenigseigg and a group of Norwegian investors plan on 
                    acquiring Saab. As of now, these brands will continue to operate 
                    as normal so there should be nothing to fear in case you are 
                    planning on purchasing one. This is because when deals like 
                    this take place it is a general practice for the new owner 
                    to utilise the existing dealer network.<br>
                    <br>
                    <br>
                    <strong>On course</strong><br>
                    Another key fact to note is that GM operations in the Middle 
                    East are not part of the US chaper 11 filing, which is restricted 
                    to the US alone. &ldquo;GM in the Middle East operates under 
                    the name of GM Overseas Distribution Corporation. This is 
                    a self-funded, vibrant and profitable company and we will 
                    continue to conduct business normally throughout our dealer 
                    network, as we have done over the years.&rdquo; says Agarwal. 
                    As such GM dealers across the Middle East are open for business 
                    as normal and all current and future owners of GM cars can 
                    rest assured that all warranty, service and parts commitments 
                    given at the time of purchase will be honoured.<br>
                    <br>
                    To prove this point GM is well on track with all their product 
                    plans for the coming months. The iconic new Chevrolet Camaro 
                    should be entering dealerships somewhere around July, the 
                    new Cadillac SRX, Malibu and all-new Cruze will make their 
                    regional debuts in October 2009, and finally the new GMC Terrain 
                    is on track for its December launch. In addition to this, 
                    GM has invested in a parts distribution centre in Dubai that 
                    will even deliver parts overnight if needed. What more assurance 
                    could one ask for?<br>
