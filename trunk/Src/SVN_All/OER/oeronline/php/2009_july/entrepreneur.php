                    </font><b><font face="Verdana" size="4">Small Office Home 
                    Office </font><br>
                    <br>
                    </b> <font face="Verdana" size="1">Given various family commitments 
                    and societal norms starting a business out of one’s home is 
                    an option that women in the Sultanate can consider seriously. 
                    Rana Alaw<img src="images/ep3.jpg" width="150" height="133" hspace="9" vspace="3" align="right">neh 
                    profiles four women entrepreneurs who have done so successfully 
                    and moved on Many forces keep Arab women away from being active 
                    participants in the economic cycle of their countries. With 
                    women making up half of any society it is important that they 
                    are involved with the economic process of any country. A number 
                    of women in the Sultanate lack the guidance of an agency that 
                    can help and provide them with the required support and finance 
                    for their ventures. Given such odds a number of women have 
                    started using their homes as a base to start a business. <br>
                    <br>
                    The government on its part is working on helping Omani women 
                    to start their own businesses. Says Sahar Al Kaabi, member 
                    of the board, Oman Chamber of Commerce and Industry and chairperson 
                    of Human Resources and Work Market Committee, “From a social 
                    perspective the government leaves no stone unturned to reduce 
                    the number of unemployed in our country. Home business is 
                    a good idea and contributes in creating a large number of 
                    job opportunities. Moreover, working from home helps Omani 
                    women to maintain her traditions and habits and avoid any 
                    inconvenience that may be caused from being away from home 
                    for long hours.” <br>
                    <br>
                    A home based enterprise needs support to expand into a small 
                    and medium sized enterprise. Getting a legal status will help 
                    such ventures to benefit from the finance and <br>
                    We profile four women entrepreneurs who started a business 
                    out of their homes only to go onto bigger things.<br>
                    support provided by different organisations to small ventures. 
                    The government is working on giving such businesses a legal 
                    standing. Says Sahar, “The law will provide support to women 
                    by granting them permits to work from home and help them to 
                    receive training and find marketing outlets for their products.” 
                    <br>
                    <br>
                    <b>Turning dreams into reality<br>
                    </b>Munira Al Nabhani, the winner of the Shell’s Intilaaqah 
                    Entrepreneur of the Year Award in 2008 is one such example. 
                    Munira started out with a one room studio in her house, spending 
                    RO500 on buying the initial photography equipment. Her hard 
                    work paid off and in four years she established her reputation 
                    as a professional photographer in the market. By the end of 
                    2006, she had rented a shop in the commercial area behind 
                    Hatat House in Muscat.<br>
                    <br>
                    Talking about the need to shift premises she says, “My customer 
                    base was growing. Moreover, whenever a lady came to my house 
                    accompanied by her husband or brother, it caused some sort 
                    of a inconvenience. So I felt it would be better if I had 
                    my own studio.” The move has also paid off financially – while 
                    she earned RO500-800 per month earlier, now the figure has 
                    jumped to RO1,000 per month.<br>
                    <br>
                    Emboldened by her success she is planning to open a new studio 
                    in Al Khuwair. Having started off with shooting wedding and 
                    other family events, she has graduated to taking pictures 
                    of official events and ministry functions. A firm believer 
                    in reinvesting in her business, Munira constantly upgrades 
                    her equipment. She says, “I will use the RO5,000 award from 
                    Intilaaqah to expand my business and to move to another area 
                    where the business potential is more.”<br>
                    <br>
                    Munira has converted a childhood passion for photography into 
                    a lucrative career. And she acknowledges the help of her family. 
                    “I trained at the Omani Association for Fine Arts before starting 
                    out as a professional and have received considerable support 
                    from my family. My husband helped me in opening and setting 
                    up the accounting and cost systems in my new shop.”<br>
                    <br>
                    <b>A whiff of freshness<br>
                    </b>Noor Al Barami, owner, Al Shmoukh Perfumes and Incense 
                    is another example of someone who has overcome daunting challenges 
                    with hard work, dedication and entrepreneurship. Says Noo<img src="images/ep4.jpg" width="150" height="209" hspace="9" vspace="5" align="right">r, 
                    “I noticed that most people preferred to use Dhofari perfumes 
                    due to their high quality and integrity, unfortunately there 
                    are not too many options in Muscat to get this kind of perfume 
                    or incense.” Having seen her mother and grandmother making 
                    such perfumes at home, she always cherished the desire to 
                    take these to a larger market. Busy with raising her children 
                    and other family commitments, she kept putting aside the idea. 
                    “Once my children grew up and became self reliant, I went 
                    ahead with my plans.”<br>
                    <br>
                    Noor started out at home with a seed capital of RO200-400. 
                    Encouraged by the initial success she wanted to move to a 
                    bigger premise. This raised the issue of finance, “To establish 
                    a shop I needed bigger capital as I had to pay rents, salaries 
                    and provide considerable quantities for display.” She borrowed 
                    money from her friends and relatives to raise her share of 
                    the RO25,000 that was required to lease a shop at the Jawharat 
                    Al Shatti mall. Nora co-owns the shop along with one of her 
                    friends. “I did not get financial support from any authority. 
                    I am now working on a feasibility study to submit it to the 
                    respective departments to get the required finances to expand 
                    our business.” <br>
                    <br>
                    To maintain quality Noor uses the best ingredients in her 
                    perfumes. This has helped in establishing a reputation for 
                    her products. Having moved out of her small office home office 
                    (SOHO) she feels that managing a home business is easy as 
                    you are in direct contact with customers, most of them being 
                    family members, friends or acquaintances. But opening a shop 
                    has created the need to adopt different marketing strategies 
                    to ensure customer loyalty. <br>
                    <br>
                    For example, she took part in various exhibitions organised 
                    by Dar Al Atta and the Muscat Festival. Noor has created certain 
                    perfumes and incenses that are named after members of the 
                    royal family (after getting their permission) and the idea 
                    has met with customer approval. In an effort to differentiate 
                    her products she started using high quality crystals and miniatures 
                    reflecting Omani tradition in perfume bottles. Noor sums up, 
                    “Despite tough competition we have managed to create a name 
                    for ourselves by producing high quality products that reflect 
                    our culture.” <br>
                    <br>
                    <b>Never say die<br>
                    </b>Persistence leads to s<img src="images/ep1.jpg" width="150" height="181" hspace="9" vspace="4" align="left">uccess 
                    – this has been the motto for Samira Al Ghaithi, owner of 
                    Lovely Lace for home decors. A series of failures in selling 
                    abayas, night dresses, scarves and cotton made items did not 
                    deter her from her pursuit of being an entrepreneur.<br>
                    <br>
                    On a vacation to Malaysia, she came across home décor items 
                    under the Lovely Lace brand name. On an impulse she bought 
                    a few pieces for RO100 and tried to sell it to her relatives 
                    and friends. The positive response saw her going back to Malaysia 
                    within six months and this time she returned with a large 
                    number of home décor items.<br>
                    <br>
                    As the business grew she felt that she needed more space to 
                    grow the enterprise. She opened a shop at Al Araimi Complex 
                    at a cost of RO40,000. Says Samira, “The turnout at the shop 
                    has been good. Although my profit is less compared to when 
                    I operated out of home, I am very happy with my success till 
                    now.” Samira has made a conscious effort to keep her margins 
                    low to attract customers. “Customers have been attracted by 
                    the fact that the price between the items in Malaysia and 
                    here are almost the same.”<br>
                    <br>
                    The demands of the business makes it difficult for her to 
                    spend as much time with the family as earlier, but she makes 
                    an effort to split her time between the shop and home. Managing 
                    a shop is different from managing a home business in other 
                    ways too, “I have to spend more on ads for publicity,” says 
                    Samira.<br>
                    <br>
                    A supportive family makes her job easier. Her husband provided 
                    her with the money to buy her first consignment and encouraged 
                    her to establish her outlet. Her son ensures good and smooth 
                    communication with exporters in Malaysia. “I advise every 
                    women not to succumb to failure but keep trying till you turn 
                    your dreams into reality,” says Samira. That’s a message that 
                    is sure to inspire a number of others to follow suit.<br>
                    <br>
                    <b>A flying start <br>
                    </b>Amal Jawad Sultan, owner of Faisal Jewellery, got the 
                    idea of opening a jewellery shop from her husband who has 
                    had a long experiences in the field. She started her venture 
                    in 2003 with an initial corpus of RO5,000. The money was spent 
                    on buying jewellery and showcasing them to her relatives and 
                    friends. In a short span of time she managed to promote a 
                    considerable number of items and saved enough money to buy 
                    her first complete collection of jewellery.<br>
                    <br>
                    She expanded the business by getting bank loans. Says Amal, 
                    “These bank facilities have helped me in setting up and expanding 
                    my business.” Amal has recently opened a store at the Capital 
                    Commercial Center (CCC) which is named after her son – ‘Faisal’.<br>
                    <br>
                    She attributes her success to good communication skills and 
                    having a place in her house to display jewellery. A focus 
                    on delivering good quality products at a reasonable price 
                    have been other factors that have contributed to her success. 
                    Amal takes every possible opportunity to promote her products. 
                    “One day I heard that Dar Al Atta was organising a fashion 
                    show. I proposed the idea of showcasing some of my jewellery 
                    at the show and it was received very well. I have also participated 
                    at a number of exhibitions that showcase different styles 
                    of abayas and jewellery,” she says.<br>
