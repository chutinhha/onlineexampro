                <td height="18"> <b></b> <p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4"><br>
                    With the release of the stylish new Soul, Cerato and now the 
                    Optima, KIA is looking to change its image. </font></b><font face="Verdana" size="1"><br>
                    <br>
                    <br>
                    <b>But has it worked? Malcolm Xavier Crasta </b><img src="images/at1.jpg" width="300" height="206" hspace="5" vspace="5" align="left"><b>writes</b><br>
                    Stylish BargainUntil now KIA&rsquo;s were attractive buys 
                    primarily because of their price, reliability and ease of 
                    maintenance and earned a good reputation in this regard. But 
                    in today&rsquo;s visual dominated world &lsquo;style&rsquo; 
                    is everything and unfortunately this was never one of KIA&rsquo;s 
                    prime aspects. Now though KIA is looking to change that by 
                    taking a whole now approach to their car designs.<br>
                    <br>
                    Take for instance the car that we are testing this month &ndash; 
                    the Optima. One look at the exterior and it is crystal clear 
                    that this is a far more attractive car than its predecessor. 
                    The Sharp angular design is much better shift in design trend 
                    from the previous bulbous designs. The design of KIA&rsquo;s 
                    new signature grille, is simple yet attractive and goes a 
                    long way in accentuating its improved looks; kudos to the 
                    designer for keeping it simple yet stylish. In addition to 
                    the grille, there are other nice touches such as the stylish 
                    rear combination lamps, narrowed and swept back headlamps 
                    and sports inspired bumper designs. <br>
                    <br>
                    <br>
                    <b>Cool interiors</b><br>
                    Even the interior follows suit with the exterior and is a 
                    huge improvement in style, design, materials and character. 
                    There is definitely no shortage of room and can easily be 
                    placed somewhere at the top of its class. Even the centre 
                    console is uncluttered and simple in its design and the red 
                    diplay panels are a nice touch. While the basic model, the 
                    LX, does come with a decent amount of kit as standard, the 
                    model one grade above, the EX, is a far more attractive prospect 
                    as it adds some key features, ABS, sunroof, alloy wheels and 
                    more, that are well worth the price increase. And for those 
                    of you who want something better, there is always the highest 
                    grade, the 2.7 EX, which is even powered by a V6 engine.<br>
                    <br>
                    Either KIA has been listening to driver feedback or has just 
                    struck it lucky, either way it shows. The driving position 
                    is surprisingly good and even the seats are comfortable and 
                    easily adjustable. All controls are within easy reach and 
                    simple to understand and from the drivers seat the visibility 
                    was good all-round. On the road the Optima has its pro&rsquo;s 
                    and its cons. For starters, the steering is sharp and responsive. 
                    The decent grip levels and a well developed suspension system 
                    strikes a good balance between sporty handling and comfortable 
                    cruising, making it quite a fun drive around town. However, 
                    the car feels slightly sluggish whenever we wanted a sudden 
                    burst of speed, say for a quick overtaking manoeuvre, but 
                    we assume that this is attributed more to the gearbox than 
                    the engine. The 2.0l inline-4 provides more than ample power 
                    in the form of 164hp and even provides a decent amount of 
                    torque. We feel that the main reason is the gearbox&rsquo; 
                    tendency to be a little slow on the downshift, a manual transmission 
                    would have fared much better in this regard and would have 
                    brought out the true potential of the engine, albeit at the 
                    cost of some &lsquo;automatic&rsquo; convenience. Aside from 
                    this the only other minor complaint is that, on hard acceleration 
                    the engine noise can be a little intrusive but at normal highway 
                    speeds it is well within acceptable levels.<br>
                    <br>
                    <strong>Money&rsquo;s worth</strong><br>
                    While the Optima may not have any one especially redeeming 
                    aspect, it is well above average in nearly all of them; and 
                    is especially worth considering from a price standpoint. Think 
                    of it sort of like a stylish jack of all trades. Add to this 
                    KIA&rsquo;s decent record when it comes to safety and reliability 
                    and you have got yourself a pretty solid buy. Our personal 
                    suggestion, if you decide to purchase an Optima, is to go 
                    one up from the basic and aim for the mid-range model if possible. 
                    While the basic is fine in itself, the EX has some attractive 
                    features that are worth paying the extra cash for, especially 
                    the addition of anti-lock brake and alloy wheels. Overall, 
                    KIA has struck its mark when it comes to offering a stylish 
                    design and at the same time has improved on its predecessor 
                    in every other aspect as well. It is definitely worth looking 
                    into. </font></p>
