		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Oman’s futuristic 
                    new tax law</font></b><font face="Verdana" size="1"><br>
                    <b>This article is aimed at providing readers with an overview 
                    of the nuances and ramifications of new income tax law that 
                    was issued recently<br>
                    <br>
                    </b> A new tax law was promulgated by Royal Decree 28/2009 
                    and published in the official gazette on June 1, 2009. The 
                    new law appears to be clear and easy to understand. There 
                    are a nu<img src="images/view1.jpg" width="103" height="121" hspace="9" vspace="3" align="right">mber 
                    of key provisions that require detailed executive regulations 
                    to provide clarification on the interpretation and application 
                    of the law. These are expected to be issued before the end 
                    of 2009<br>
                    <br>
                    <b>Start date</b><br>
                    The new law takes effect from tax year 2010 which starts on 
                    January 1, 2010. An appropriate assumption would be that, 
                    for a majority of businesses which end their accounting year 
                    on December 31, they have six months before the law takes 
                    effect. There are of course differences between an accounting 
                    year and a tax year. In effect, the new tax law applies to 
                    accounting years or periods ending in tax year 2010. Consequently, 
                    for a taxpayer with an accounting year ending on 31 March 
                    2010, the new tax law applies from April 1, 2009.<br>
                    <br>
                    <b>Filing of business particulars<br>
                    </b>A compliance matter that would require early attention 
                    is the requirement for all taxpayers to ensure that their 
                    business particulars filed with the tax department are current 
                    and updated within three months of any change. Non compliance 
                    may result in a fine not exceeding RO2,500. This means that 
                    all existing businesses must ensure that the business particulars 
                    that they may have filed with the tax department must be current 
                    as on January 1, 2010.<br>
                    Who is taxable<br>
                    <br>
                    The tax law continues to apply to Omani establishments or 
                    registered sole proprietorships, companies and permanent establishments 
                    (PE) of foreign companies. <br>
                    <br>
                    <b>Service PE<br>
                    </b>To the relief of many foreign companies that carry on 
                    business in Oman for short duration, Oman has demonstrated 
                    considerable business sense by introducing the internationally 
                    accepted concept of a service PE. Under the new law, a foreign 
                    company will be considered to create a PE only if services 
                    are rendered in Oman for a period or periods exceeding 90 
                    days in the aggregate in any 12 month period.<br>
                    <br>
                    In the past, foreign companies making a short trip to Oman 
                    to undertake limited work or duration services in connection 
                    with one off contracts or short term projects in Oman were 
                    deemed to create a PE and were hence taxable. This change 
                    relieves many foreign companies who may visit Oman to complete 
                    installation, commissioning, maintenance, testing, survey 
                    or sample collection or consulting services from the burden 
                    of filing tax returns. On the other hand, certain of these 
                    services may come within the new withholding tax provisions, 
                    in which case, the Omani taxpayer will have an obligation 
                    to withhold flat rate tax of 10 per cent from payments made 
                    for such services.<br>
                    <br>
                    <b>Dependent agents are now accountable for tax <br>
                    </b>With a carrot and stick approach, the service PE relief 
                    has been countered with wider definition of permanent establishment 
                    of a foreign company to include dependent agents. Where, in 
                    substance, a foreign principal carries on a business through 
                    an agent the local agent may be considered to be a PE of the 
                    foreign company and taxable. These provisions, which also 
                    deem the local agent to be principal officer of the foreign 
                    company, require careful consideration.<br>
                    <br>
                    To reinforce the above measures, it has been made clear that, 
                    the burden of tax cannot be transferred to anybody by the 
                    entity which is subject to tax. This implies that, under the 
                    new law, foreign companies may not be able to shift the burden 
                    of tax obligations to the Omani companies.<br>

                    <br>
                    <b>Services subject to withholding tax broadened<br>
                    </b>In an effort to narrow revenue leakages, Oman has aligned 
                    itself with more developed tax jurisdictions by substantially 
                    broadening the scope of services subject to withholding tax 
                    applicable to incomes accruing or arising from Oman to foreign 
                    companies with no presence in Oman. A flat rate withholding 
                    tax of 10 per cent will apply to payments for royalties, research 
                    and development, computer software and management fees. The 
                    definition of royalties has also been extended to include 
                    the use or right to use intellectual or proprietary rights 
                    including computer programmes, any media used for radio or 
                    television broadcasting, payments for industrial, commercial 
                    or scientific equipment or experience and rights to work mineral 
                    or other sources of natural wealth.<br>
                    <br>
                    <b>Depreciation rules simplified<br>
                    </b>Another change much welcomed by overworked financial managers, 
                    are the rules relating to tax depreciation which have been 
                    simplified. Depreciation of assets other than buildings will 
                    now be calculated on a pooling (or block) of assets basis.<br>
                    Assets are classified into three pools with each asset base 
                    calculated with reference to opening value plus additions 
                    minus sale proceeds of disposals. Tax depreciation rates have 
                    been maintained with the exception of accelerated depreciation 
                    for computers and a significantly reduced rate for drilling 
                    rigs which are now to be depreciated on a reducing balance 
                    basis at 331/3 per cent and 10 per cent respectively.<br>
                    <br>
                    The pooling of assets significantly decreases the compliance 
                    work involved in preparing yearly tax returns but without 
                    compromising the efficiency of tax depreciation provisions. 
                    Buildings will continue to be depreciated on a four per cent 
                    straight <br>
                    line method.<br>
                    <br>
                    <b>Pre incorporation income and goodwill <br>
                    </b>To remove previous tax law uncertainties and clarify recent 
                    tax practice, gains on disposal of goodwill or commercial 
                    name or trademark has been deemed to be taxable income. This 
                    is a complex area as this change may also impact business 
                    reorganisations and transfers of contracts between group companies. 
                    Incomes accruing to an Omani company before its incorporation 
                    or registration have also been deemed to be taxable.<br>
                    <br>
                    <b>Tax exemptions and other incentives <br>
                    </b>To encourage growth of industries, development of tourism 
                    and other strategic sectors, tax exemptions are available. 
                    Qualifying manufacturing businesses and projects of strategic 
                    economic importance with substantial export and employment 
                    potential continue to enjoy tax exemptions. These exemptions 
                    are available for upto 10 years. <br>
                    <br>
                    <b>Foreign Incomes now taxable<br>
                    </b>Another change, particularly unpopular with larger Omani 
                    companies, is the introduction of a global tax regime. Under 
                    the new laws, all incomes including overseas income earned 
                    by an Omani company will be taxed in Oman. However, a tax 
                    credit limited to Oman’s tax rate of 12 per cent will be available 
                    as offset.<br>
                    <br>
                    <b>Cross border and tax avoidance transactions <br>
                    </b>Once again Oman has aligned with tax practices in more 
                    developed jurisdictions by introducing specific provisions 
                    relating to complex cross border transactions between related 
                    parties and tax avoidance transactions wherever arising. The 
                    complicated issue of pricing between related parties has been 
                    addressed by introducing requirements to meet accepted transfer 
                    pricing concepts to prove arms length dealings between multinational 
                    companies and their Oman businesses. There is also some tightening 
                    with allowable deductions with restrictions on deductibility 
                    of head office costs which would now include costs related 
                    to affiliate companies.<br>
                    <br>
                    Another innovative concept from advanced tax regimes called 
                    thin capitalisation will also apply to deductions for interest-cost-on-loans. 
                    These would need to meet thin capitalisation rules to be introduced 
                    by executive regulation. Basically, when the shareholders 
                    use excessive loans funding instead of capital contributions 
                    to fund the capital requirements of a company, the company 
                    is referred to be thinly capitalised. The concept assumes 
                    that excessive shareholder loans allow the shareholders to 
                    pay out profits in the form of interest which is then tax 
                    deductible. Finally, the new laws also include provisions 
                    for issue of executive regulations, ministerial rulings and 
                    clarifications by the secretary general for taxation to improve 
                    application or tax administration.<br>
                    <br>
                    <b>Compliance and enforcement matters Service of notices<br>
                    </b>The tax law also clarifies that a notice from the tax 
                    department is deemed to be served within 15 days from the 
                    date of letter. This is significant as deadlines for filing 
                    appeals, objections and tax cases depend on the date of notification 
                    of a decision. For determination of filing due dates, if a 
                    statutory deadline falls on a public holiday, the deadline 
                    is now extended to the <br>
                    next working day.<br>
                    <br>
                    <b>Principal officer<br>
                    </b>There are a number of major changes to definition and 
                    functions of a principal officer. The law now defines who 
                    is regarded as principal officer for various entities. In 
                    this regard, the law now deems an Omani agent as a principal 
                    officer where a foreign company carries on activity in Oman 
                    through a dependant agent. This provision may have significant 
                    implications for Omani companies that act as agents for foreign 
                    companies. It is also specifically stipulated that a sole 
                    proprietor or owner of a permanent establishment who is outside 
                    Oman, shall designate a principal officer to comply with the 
                    obligations under this law. Such principal officer shall not 
                    be absent from Oman for more than 90 days in a tax year.<br>
                    <br>
                    <b>Appellate process<br>
                    </b>There are no major changes to existing appellate processes 
                    for procedures relating to filing an objection and appeals 
                    with a time limit of 45 days continuing to apply for submissions. 
                    Tax payers may also seek an extension of time for payment 
                    of disputed tax. Undisputed tax is payable within 30 days 
                    from the date of objection.<br>
                    <br>
                    <b>Penalties</b><img src="images/view4.jpg" width="200" height="126" hspace="9" vspace="1" align="right"><b><br>
                    </b>To encourage compliance, the new law also includes specific 
                    administrative and criminal penalties and punishments for 
                    non-compliance of the law and other offences. Where the tax 
                    payer fails to submit or delays the submission of tax returns 
                    the secretary general can impose fines ranging between RO100 
                    and RO1,000. Where the tax payer fails to declare correct 
                    taxable income in the tax return for any tax year, a fine 
                    of not more than 25 per cent of the difference between the 
                    amount of tax determined on the correct taxable income and 
                    the amount of tax as per the return submitted may apply.<br>
                    <br>
                    Penalties are also prescribed for non-submission of the information 
                    required under the law, failure to attend meetings or respond 
                    to queries raised by the tax department. Criminal penalties 
                    of upto RO5,000 and imprisonment for intentional offences 
                    are also prescribed for serious offences.<br>
                    <br>
                    In conclusion, Oman can boast of embracing the global economy 
                    by implementing international tax concepts and a low tax rate 
                    of 12 per cent that now applies to all businesses both local 
                    and foreign. This level playing should encourage better tax 
                    compliance especially when coupled with penalties for non 
                    compliance.<br>
