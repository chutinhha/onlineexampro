                <b>Bank Muscat exposures to Al Gosaibi Group and Saad Group</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    Two large groups in Saudi Arabia, the Al Gosaibi Group and 
                    the Saad Group, have defaulted on their payment obligations 
                    across a number of banks. BankMuscat stated in a release that 
                    it has a direct exposure to these groups amounting to approximately 
                    RO49mn through its Riyadh Branch. The bank added that one 
                    of its associates, BMI Bank, Bahrain, also has exposure to 
                    these groups, amounting to approximately RO17mn. The exposure 
                    of BM and BMI Bank is to the operating companies of the group 
                    but it has no exposure to The International Banking Corporation, 
                    Bahrain or Awal Bank, Bahrain which form part of these groups. 
                    Though these groups are in default on some of their obligations, 
                    the bank takes comfort from the fact that the groups&rsquo; 
                    operating companies are profit making and based on the last 
                    published financials, have strong balance sheets. Further, 
                    given the prominence of these groups, the size of their debts 
                    and the number of international and regional banks involved, 
                    BankMuscat is confident that the concerned governments and 
                    regulators would take proactive steps to ensure an orderly 
                    settlement of obligations.<br>
