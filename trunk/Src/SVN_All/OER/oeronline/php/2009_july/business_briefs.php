                    services system for a period of 25 years, with frequency spectrum 
                    rights granted for 15 years. HE Sheikh Salim bin Mostahail 
                    Al-Mashani, chairman of the board, Nawras, said: &ldquo;The 
                    award of the new license is hugely significant for Nawras, 
                    providing us with the legal framework to enable our company&rsquo;s 
                    transformation into a full-service, integrated telecommunications 
                    provider for Oman.&rdquo; HE Sheikh Abdullah Bin Mohammed 
                    Bin Saud Al Thani, chairman, Qtel said: &ldquo;This is an 
                    historic moment for Nawras and the Qtel Group, as it enables 
                    us to offer a full range of services to the people of Oman, 
                    and to deliver our commitment to quality for fixed line customers 
                    as well as mobile customers.&rdquo; <br>
