                    Class License for the installation and operation of a common 
                    fixed telecommunications services system. As license holder, 
                    Nawras is now entitled to provide fixed, data and international 
                    telecommunication services for customers in Oman, and can 
                    use its network infrastructure to offer a range of leading-edge 
                    technologies, such as Wi-Max and HSDPA services. The license 
