                      in the Middle East, and what the Middle East is doing to 
                      the world, says Ganesh Sundararaman</b><br>
                      <br>
                      Ms Wright is right, when she says “Islamic terrorism is 
                      no longer the most interesting, or dynamic force, in the 
                      Middle East. Islamic politics has surfaced”.<img src="images/img75.jpg" width="150" height="276" hspace="9" vspace="5" align="right"><br>
                      <br>
                      The hard-core terrorists of Al Qaeeda or Islamic Jihad have 
                      repeatedly proven that they can destroy. But they have yet 
                      to provide tangible solutions or viable new models for problems 
                      plaguing the region. Regimes are increasingly unable to 
                      provide what they promised to protect their people, and 
                      they certainly provide no direction for the future.<br>
                      <br>
                      Robin Wright’s elaborate perspective of the Middle East 
                      is obviously born out of having reported from the region 
                      for years. In Dream &amp; Shadows, she moves from one state 
                      to another, interspersing her commentary with prolific interviews 
                      with important persons in each region, particularly political 
                      personalities.<br>
                      <br>
                      Searching assessments of the region’s complicated political 
                      and economic situation include “You cannot assume the role 
                      of business in the peace process; it has to be proven… Peace 
                      will be a two state solution but it cannot end there. We 
                      need real economic cooperation, integration, and compensation.”<br>
                      <br>
                      The Middle East is not really one place, so change will 
                      have many faces. “Democracy” said Paul Salem, “will be a 
                      country-by-country phenomenon”. Disdain is widespread. One 
                      reads about ruptures everywhere. The splitting and the reorganisation 
                      of the PLO had been an elaborate exercise for Yasser Arafat. 
                      But that he was not popular was established by a 2004 survey 
                      that showed 87 per cent of the people felt his government 
                      was corrupt, and that its leaders were opportunists.<br>
                      <br>
                      All in all, Ms Wright’s picturisation of the Middle East 
                      reflects gaping diversities in ideology. The difference 
                      between fundamentalist, Islamist and an Islamic reference 
                      often seem nuanced. Definitions also vary by part; context 
                      varies by country. But the distinction is the essence of 
                      an emerging political trend in the Middle East. The most 
                      significant comment she leaves the readers with is the hope 
                      about the end of American influence in the Middle East…after 
                      Iraq. <br>
                      <br>
                      Robin Wright’s Dream &amp; Shadows is not one of those un-put-downable 
                      thrillers. A must-read for those that have the interest 
                      to look beyond everything one sees right now.</font><font face="Verdana" size="1"><br>
