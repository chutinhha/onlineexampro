				</b></font><font face="Verdana" size="1">There are various 
				indicators to measure the health of an economy. One could use 
				hard statistical data like credit uptake, corporate results, 
				government spending or oil prices. On October 21st oil touched a 
				yearly high of $82 per barrel. Consolidated profit of the MSM30 
				companies stood at RO126mn in Q2 2009 – 13.4 per cent higher 
				compared to Q1, 2009. Analysts expect these numbers to improve 
				during Q3 2009, due to sequential gains and the base effect. 
				Across the board, in manufacturing and services, there seems to 
				be much cause for cheer. Cement sales are up and so are imports 
				through the Sultanate’s ports. <br>
				<br>
				The Tender Board has been busy signing contracts. The government 
				awarded contracts worth RO2.3bn by mid-October 2009. Analysts 
				expect another RO3.2bn worth of projects to be awarded in the 
				next few months, taking up the value of such projects to RO5.5bn 
				by the end of 2009. Equally important are the soft indicators. 
				The surge in footfalls that you can see in the shopping malls is 
				a sure pointer that consumers are feeling good about spending, 
				yet again. Going by the increasing number of vacancy ads in the 
				papers one can safely assume that the job market is looking up. 
				Prices in the real estate sector have also started firming up.
				<br>
				<br>
				But the biggest indicator that sentiment is getting better comes 
				from the stockmarket. The MSM 30 index closed on September 30 at 
				6,572 points – a year-to-date gain of 20.8 per cent. The month 
				also saw the index touching a yearly high of 6,737. Investors 
				both domestic and foreign have been coming back to the market in 
				hordes pushing up the average volume of daily trade. Market 
				multiples are up and valuation of stocks have seen a sharp 
				re-rating. <br>
				<br>
				The good news finds a resonance across the globe. According to 
				the latest IMF estimates, global GDP will expand by 3.1 per cent 
				in 2010, 1.2 percentage points faster than it forecast in April. 
				Global stockmarkets have rallied by 64 per cent since their 
				trough and production has stopped shrinking in all the world’s 
				big economies. Given the mood, one can safely assume that the 
				slowdown is well and truly behind us. Our cover article takes a 
				looks at this unfolding story. <br>
				<br>
