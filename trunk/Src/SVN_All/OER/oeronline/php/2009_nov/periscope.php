                    Natural Gas Shares</font></b><font face="Verdana" size="1"><img border="0" src="images/img25.jpg" align="right" hspace="8" vspace="3" width="80" height="94"><br>
                    <strong>Natural gas prices are cheaper than crude oil prices 
                    and the directional bias is the same as global industrial 
                    production boosts electricity demand. Moreover, as the auto 
                    sector revives worldwide and environmental activists target 
                    dirty energy such as coal, natural gas prices can only benefit 
                    by Matein Khalid</strong><br>
                    <br>
                    <br>
                    Natural gas, without a swing producer such as Saudi Arabia 
                    to put floor under prices, plummeted as the world’s industrial 
                    production sank after the failure of Lehman and inventories 
                    reached storage capacity in the US. However, short covering 
                    and speculative buying by global macro hedge funds has led 
                    to a spike above the recent bottom in natural gas (natgas) 
                    at $3 per million British thermal units (BTU). Natural gas 
                    prices are cheaper than “energy equivalent” crude oil prices 
                    since black gold is far more responsive to global economic 
                    growth, OPEC supply cuts, risk appetites and the depreciation 
                    in the dollar. A natgas recovery will lag crude oil recovery 
                    but the directional bias is the same as global industrial 
                    production boosts electricity demand. This is medium term 
                    bullish for natgas prices.<br>
                    <br>
                    Moreover as the auto sector revives worldwide and environmental 
                    activists target dirty energy such as coal being used by China, 
                    natural gas prices can only benefit. <br>
                    <br>
                    <b>Supply and demand </b><br>
                    The bullish case for natural gas rests on both the supply 
                    and demand curve. As natgas prices sank, North American drilling 
                    contracted by almost 60 per cent, the worst gas drilling contraction 
                    since the oil crash of 1998/99. It is not unrealistic to expect 
                    natural gas will double from their September bottom, a pattern 
                    seen after the natgas crash of both 1999 and 2001 as industrial 
                    demand recovered from the economic recession. This is all 
                    the more true since the focal print of global industrial growth 
                    in 2010 has shifted to the emerging markets of the Pacific 
                    Basin, Brazil and India. I doubt if LNG cargos from Qatar, 
                    Indonesia or Nigeria will cap the bullish momentum in natgas 
                    prices. <br>
                    <br>
                    Moreover, shale oil (unconventional gas) or coalbed methane 
                    ca<img border="0" src="images/img27.jpg" align="right" hspace="8" vspace="3" width="305" height="491">nnot 
                    substitute for natural gas in the short run. It is thus entirely 
                    reasonable to expect natural gas prices to surge to US$8 per 
                    million BTU by end 2010. An American or Israeli strike on 
                    Iran could even cause an immediate geopolitical panic premium 
                    in natgas prices. The global campaign against carbon emission 
                    will only increase the appeal of natural gas in the energy 
                    mix relative to coal, crude oil and oil sands.<br>
                    <br>
                    <b>Takeover mania</b><br>
                    It is difficult for GCC private investors to play Russian 
                    roulette in the natural gas futures markets. Natural gas exploration 
                    and production companies offer for more upside than simply 
                    buying and rolling over a NYMEX futures contract, particularly 
                    as steep contango means steeply higher prices for far end 
                    contracts. The January 2010 natgas futures contract prices 
                    a $6 per million BTU gas price. It is far more rational to 
                    buy shares of natural gas companies on the NYSE who are prime 
                    candidates for takeover bids from Big Oil. BP, ENI, Total, 
                    Statoil and British Gas have made no secret of their desire 
                    to acquire natural gas and even shale oil acreage in the US. 
                    This means takeover mania in US natural gas is inevitable. 
                    Natgas producers with the lowest cost and highest capex/free 
                    cash flow are takeover bait. Oklahoma’s Devon Energy (DVN) 
                    is a classic takeover candidate with one of the lowest unit 
                    costs in the natgas sector with valuation metrics of 11 times 
                    earnings and 1.5 times book value. It would be optimal to 
                    buy Devon Energy in the 56-58 range for an 80 target in the 
                    12 months.<br>
                    <br>
                    <b>Russian route</b><br>
                    Russia’s Gazprom is one of the world’s iconic gas companies, 
                    owner of one fifth of the planet’s gas reserves and the Kremlin’s 
                    geopolitical weapon in its relations with the EU, China and 
                    Ukraine. Prime Minister Putin’s visit to Beijing and the subsequent 
                    gas deal with the Middle Kingdom could be bullish for Gazprom 
                    shares, that trade as New York ADRs as well as in Moscow. 
                    After all, China would act as a new export market for Gazprom, 
                    whose only real current export market is Europe. The exact 
                    pricing formula that will determine Gazprom volumes to China 
                    has still not been disclosed by either the Kremlin or Beijing. 
                    It is also doubtful if China will allow its industrial constellation 
                    to become dependent on Russian gas imports, unlike the EU. 
                    China has signed LNG contracts and has negotiated gas deals 
                    with several Central Asian producers as well as boosted its 
                    own domestic gas production capacity. I believe the current 
                    euphoria on Chinese gas has inflated the value of Gazprom 
                    ADRs to unjustifiable levels. I would sell Gazprom ADR short 
                    at 25 for a target below 20.<br>
                    <br>
                    Gazprom is the largest natural gas producer in the world, 
                    with gas reserves that exceed 100 billion barrels per oil 
                    equivalent and enjoys a monopoly in Russian gas exports. Yet 
                    Gazprom shares are now far too expensive at current levels.<br>
