                    </font><b><font face="Verdana" size="1"> </font></b> <font face="Verdana" size="1"><b>For 
                    a vehicle not originally designed for the luxury SUV market, 
                    Land Rover’s Range Rover has come a long way in the past 39 
                    years and has built up quite a strong reputation for itself 
                    in the very same segment. Malcolm Xavier Crasta finds out 
                    if the new 2010 Range Rover lives up to its name<br>
                    <br>
                    </b> <img src="images/img58.jpg" width="400" height="268" hspace="8" vspace="5" border="0" align="right" style="border: 1px solid #E9E9E9">Currently 
                    in its third generation, launched in 2002, the Range Rover 
                    has been through quite a few facelifts with the largest changes 
                    taking place in 2006, when the older BMW sourced engines were 
                    replaced by Jaguar power plants. For the 2010 model as well, 
                    Land Rover has taken things up a notch with quite a few significant 
                    upgrades that make an already good SUV simply amazing.<br>
                    <br>
                    Despite these significant changes, not much can be said about 
                    the exterior as most of it remains quite similar to the previous 
                    model. There are subtle changes though and these work quite 
                    well to distinguish the car and give it a fresher look. These 
                    changes include an updated exterior grille, bumpers, new LED 
                    and Bi-Xenon head lights and new LED tail Lights. A majority 
                    of the changes are to the interior and mechanicals. Change 
                    or no change it is hard to argue with the Range Rover’s road 
                    presence. Step into the back seats through the rather small 
                    rear doors and you are greeted with the usual welcoming ambience 
                    of the HSE, surrounded by swathes of leather and rich wood, 
                    relaxing to the sights and sounds of your very own DVD screen 
                    and headset.<br>
                    <br>
                    St<img src="images/img60.jpg" width="150" height="224" hspace="8" vspace="5" border="0" align="left" style="border: 1px solid #E9E9E9">ep 
                    into the driver’s seat and you are immediately greeted by 
                    the first, and most obvious, indication of the 2010 Range 
                    Rover – the new 12-inch TFT-LCD virtual instrument panel. 
                    This neat little bit of kit can be both personalised and adapted 
                    to suit the current driving conditions. Aside from the instrument 
                    display and new steering mounted controls, the rest of the 
                    interior hasn’t received much of a design change but instead 
                    it has received a few significant technological upgrades and 
                    some all new options. Chief among these upgrades is the centre 
                    console mounted touch-screen navigation display. Satellite 
                    navigation is now based on a Hard Disc system that allows 
                    for more increasing reliability and accuracy thanks to data 
                    upgrades from a Land Rover Dealer. Adopting its interface 
                    from Jaguar, it now features fewer, more intuitive menus and 
                    no option is more that a few touches away. <br>
                    <br>
                    While these changes are great in their own right it is not 
                    until you get moving that the true extent of the improvements 
                    actually hit home. The first change you will notice is the 
                    sheer power of the HSE and the way in which it eats its way 
                    through the speedometer. The reason for this immense pace 
                    can be found under the bonnet where last year’s 4.4-litre 
                    V8 has been replaced by an all-new direct-injection 5.0-litre 
                    V8. The result is a staggering 70hp bump in power for a total 
                    of 375hp, just 25hp short of last year’s Range Rovers Supercharged 
                    (The new Range Rover Supercharged sports a supercharged version 
                    of the same 5.0-litre V8 producing 501hp). Despite the increase 
                    in engine size and power, it is commendable to note that the 
                    fuel economy remains unchanged. Changes have also been made 
                    to the Terrain Response System and, along with the impressive 
                    air suspension system, provides one of the best rides in its 
                    class. <br>
                    <br>
                    It is a given that most Range Rovers sold will not be taken 
                    off-road, which is a real shame considering its terrain conquering 
                    ability. The way in whi<img src="images/img59.jpg" width="314" height="201" hspace="8" vspace="5" border="0" align="right" style="border: 1px solid #E9E9E9">ch 
                    this three tonne behemoth can clamber over rocks and skim 
                    across sand is truly impressive but maybe not as much so as 
                    the ease with which it can achieve such feats. Land Rover’s 
                    Terrain Response System is truly a wonder of engineering that 
                    uses fingertip controls on the centre console to choose settings 
                    for different driving environments. This system in combination 
                    with the Adaptive Dynamics system helps ensure that all occupants 
                    are transported in the greatest possible comfort and security 
                    regardless of the road surface and variations in driving conditions.<br>
                    <br>
                    Improvements to safety have not taken a back seat either. 
                    Included in the already extensive list of safety features 
                    is the new knee airbag for the driver along with the blind-spot 
                    monitoring available to customers. Overall, the changes are 
                    quite significant and very welcome. Its good on-road manners 
                    combined with its tremendous off-road ability make the Range 
                    Rover one of the most practical choices in its segment. Combined 
                    with its wealth of hi-tech options and reasonable price, the 
                    2010 Range Rover is truly an all-terrain limousine and the 
                    one to have.<br>
