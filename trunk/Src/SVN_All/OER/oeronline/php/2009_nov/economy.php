                    </font> <b><font face="Verdana" size="4">Let’s get physical<br>
                    </font></b><font face="Verdana" size="1"><b>With the property 
                    market stigmatised and financial services still treated with 
                    abundant caution, the healthcare industr</b><b>y is in the 
                    spotlight with investment focus moving away from speculative 
                    activities towards long-term prospects like healthcare</b> 
                    <strong>By Oliver Cornock </strong><br>
                    <br>
                    <img src="images/img54.jpg" width="100" height="104" hspace="8" vspace="5" border="0" align="right"> 
                    Forget the ox, 2009 is the year of the swine. The H1N1 virus, 
                    popularly known as ‘swine flu’, has featured in headlines 
                    around the world for months. With the onset of the winter 
                    flu season in the Northern hemisphere it’s become even more 
                    prominent. In Oman and the wider GCC, the often sensational 
                    slant that the virus assumes in the media has only served 
                    to attract more interest to a sector that is already ripe 
                    with potential: healthcare. As investor appetite begins to 
                    slowly return, it is a sector likely to land squarely on the 
                    map of post-crisis investment patterns.<br>
                    <br>
                    <b>Healthcare neglected</b><br>
                    While foreign direct investment (FDI) has been rapidly increasing 
                    in Oman in recent years (it was the fastest growing destination 
                    for FDI in 2008, according to FDI I<img src="images/img55.jpg" width="145" height="389" hspace="8" vspace="5" border="0" align="left">ntelligence 
                    magazine), construction and real estate have attracted the 
                    lion’s share of funds, leaving the healthcare industry neglected 
                    by the private sector. In 2007 private sector healthcare expenditure 
                    represented just 17.5 per cent of total expenditure, according 
                    to the World Health Organisation (WHO).<br>
                    <br>
                    Healthcare is now entering the spotlight. According to a recent 
                    report from Alpen Capital, the investment arm of the Bank 
                    Sarasin-Alpen (ME), the healthcare market of the GCC is set 
                    to grow by a CAGR of nine per cent over the next 12 years, 
                    from $18bn in 2008 to $47-55bn. With the property market stigmatised 
                    and financial services still treated with abundant caution, 
                    the health industry is being flagged for its non-cyclicality 
                    and strong built-in demand. Perhaps the only positive legacy 
                    of the global financial crisis will be a lasting reorientation 
                    of investment focus away from more speculative activities 
                    and toward long-term prospects such as this.<br>
                    <br>
                    In June of this year, two new private hospitals began operations 
                    in Oman – the Al Raffah and Kims Oman Hospital – joining Muscat 
                    Hospital, the only other privately run hospital, which opened 
                    in 2000. The government still operates the vast majority – 
                    93 per cent – of hospitals, but the ranks of private facilities 
                    may soon swell dramatically. The Ministry of Health (MoH) 
                    has announced it is reviewing some 22 proposals for private 
                    hospitals in and around Muscat.<br>
                    <br>
                    <b>Healthcare demand growing</b><br>
                    But the influx of private sector attention in the healthcare 
                    industry is not merely a product of declining real estate 
                    and financial services sectors – those pre-crisis strongholds 
                    of foreign investment. As philosopher Auguste Comte noted, 
                    demography is destiny, and Oman’s population dynamics point 
                    to a growing demand for healthcare services for years to come. 
                    <br>
                    <br>
                    Much like the rest of the GCC, the population of Oman is young 
                    and growing quickly. Over half is under the age of 20 and 
                    83 per cent is under 35, while population growth measures 
                    over three per cent per annum. Traditionally, youthful countries 
                    are regarded as less desirable targets for healthcare development, 
                    as the elderly tend to command a greater demand for services. 
                    However, this sentiment is starting to recede due to the unfortunate 
                    explosion of so-called “diseases of affluence” in Oman and 
                    throughout the region that has accompanied the rapid rise 
                    in personal wealth over the past years. The World Health Organisation 
                    (WHO) predicts the incidence of diabetes in Oman to increase 
                    by 200 per cent by 2030.<br>
                    <br>
                    Changes in lifestyle and eating habits have led to a well-documented 
                    rise in the incidence of non-communicable diseases like diabetes, 
                    obesity and various cardiovascular ailments – all of which 
                    demand specialised care and preventive education programmes.<br>
                    <br>
                    <b>Addressing shortfalls</b><br>
                    Under the current five-year health development plan, which 
                    runs through next year, both preventive medicine and increasing 
                    specialisation initiatives are featured. For example the National 
                    Diabetes Centre – currently under construction and expected 
                    to open soon – seeks to address the root cause of the disease 
                    with a focus on health and lifestyle education.<br>
                    <br>
                    Another state facility under development, the RO5.2mn national 
                    genetic centre (NGC) will encompass both research and development, 
                    as well as education and treatment facilities for genetics-related 
                    issues. It could prove to be an especially important nexus 
                    of medical research and education in the region. A recent 
                    study by the Centre for Arab Genomic Studies revealed that 
                    Arabs are disproportionally pre-disposed to many genetic disorders. 
                    According to the report, over 900 genetic diseases were found 
                    to be common around the Arab world – one of the highest rates 
                    globally. While the state has certainly not neglected the 
                    development of advanced medical centres, the sheer scale of 
                    demand necessitates private sector participation. The project 
                    pipeline of the entire GCC, according to the Alpen report, 
                    is quite large, with about 200 hospitals totalling 27,000 
                    beds.<br>
                    <br>
                    The government absorbs the majority of the cost of healthcare 
                    in the Sultanate, which is provided to Omani citizens at a 
                    nominal fee. In 2008 the state allocated RO228m – or about 
                    four per cent of total expenditure – for healthcare. Under 
                    the expansionary 2009 budget, spending had increased by 19 
                    per cent to RO271m – the second largest year-on-year expenditure 
                    increase in this year’s budget.<br>
                    <br>
                    <b>Challenges ahead</b><br>
                    Despite the state’s attention to the sector, barriers to further 
                    development remain, largely in the form of shortages of skilled 
                    staff. Currently, the Sultanate relies heavily on expatriate 
                    medical professionals as the Omanisation programme and medical 
                    training facilities strive to produce enough doctors and nurses 
                    of Omani origin. The financial crisis, too, could cause some 
                    hiccups in short to medium-term sector development. While 
                    the healthcare industry itself is recession-proof, the construction 
                    industry is not and the financing options needed to build 
                    new health facilities are harder to come by.<br>
                    <br>
                    Nevertheless Oman’s economic course has remained relatively 
                    steady during the recent financial gales and the Sultanate 
                    is expected to grow by at least two per cent this year – higher 
                    than most of its GCC counterparts. With the price of oil apparently 
                    stable and predicted to go higher by the end of the year, 
                    the projected budget deficit – marked at RO810m upon its release 
                    in January – could shrink significantly. These positives will 
                    help to attract greater private sector participation in a 
                    region full of healthcare industry potential.<br>
                    <br>
                    A greater public awareness of health issues has come a long 
                    way since the start of the reign of His Majesty Sultan Qaboos 
                    bin Said (and the subsequent transformation of healthcare 
                    provision). In the meantime the demand for more specialised 
                    healthcare should remain strong, and could be significantly 
                    bolstered by the passing of a mandatory health insurance law. 
                    Speculation over the status of the law has persisted for close 
                    to a decade and it remains unclear when it will come into 
                    effect. But with or without the law, analysts around the globe 
                    will be talking about healthcare in Oman well after the H1N1 
                    spectacle eventually drifts into obscurity.</font><font face="Verdana" size="4"><b><br>
