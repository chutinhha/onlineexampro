<p align="justify" dir="ltr"><font face="Verdana" size="3" color="#FF0000">
<br>
</font><font face="Verdana" size="4">
<b>Integrated Efforts to Meet Challenges</b><br>
</font><font face="Verdana" size="1">
<br>
<b>
<img border="0" src="spotlight.jpg" width="350" height="311" align="left" hspace="5" vspace="5"></b>Even though Oman has made a remarkable progress on the development of IT, a lot 
is remains to be done. Oman has to overcome certain challenges before it can 
transform itself into a developed knowledge economy. “The investment in the 
development of IT infrastructure needs to be accelerated to make it come on par 
with global standards,” said John M. Adams, Senior Associate, Booz Allen 
Hamilton Inc., in a public forum in Muscat recently.<br>
<br>
Take a look at some of the IT related data. Oman’s ICT Use 2005 Index (as per 
Madar Research) was just 0.81, the lowest in the Gulf region. According to a UN 
report, Oman ranks a low 112 on the E-Government Readiness Index 2005. Going by 
an International Telecommunication Union report in 2005, Oman has an Internet 
Users Index of 0.105 (per 100 person) and PC Index of 0.045 (per 100 person), 
quite low by global standards. <br>
<br>
<b>Barriers to Surmount</b><br>
OER spoke to captains of Oman’s IT industry to identify the bottlenecks in the 
growth of IT in the Sultanate. “IT and communication infrastructure must get 
strengthened further. Market size is small. Although the overall economy has 
shown very positive trends in last two years, the market has to grow 
substantially. There is non-availability of adequate technology skills locally,” 
said CE Potnis, CEO, OCS Infotech.<br>
<br>
Ashok Sardiwal, CEO of IMTAC, said: “IT industry in Oman has matured over the 
last few years. The government has given due importance to IT by setting up the 
Information Technology Authority. The ITA, supported by the industry, is in the 
process of removing obstacles that hinder the growth of IT deployment in Oman. 
Some of the challenges that still need to be addressed are availability of local 
expertise, telecom infrastructure, legislative framework and IT awareness.” <br>
<br>
He further said, “ITA is on the right path to remove obstacle and spur the 
growth of the sector. The success will depend on fast deployment of various 
initiative and sustained focus. Oman is blessed with a young population, which 
can excel in the services industry. The challenge is to provide them with the 
right education and incentivise them to come up to a level where they can 
compete with the global work force. In the recent past, customers have starting 
directly dealing with non-Oman based companies for large projects. Direct deals 
with foreign companies, who do not have long-term commitments in Oman and do not 
support Omanisation, weakens well-established local companies and this will 
affect investment in building an international class local workforce.”<br>
<br>
Dhananjay Bopardikar, Professional Services Manager, Gulf Business Machines 
(Oman) Co. LLC, says “IT is today looked at as buying boxes. We haven’t seen 
many big organisations strategising on IT. In mature markets, IT is combined 
with business strategy and revisited at regular intervals. That’s not happening 
here. We would like IT not to be seen just as a back office enabler but as a 
facilitator that enhances business. It will help in the growth of IT in Oman. A 
thought leadership needs to be brought in so that organisations can start 
thinking differently.”<br>
<br>
According to M.K. Janaki Raman, General Manager, Bahwan IT LLC, “The 
infrastructure on connectivity needs to improve further mainly in the interior 
areas. Unless we have a good telecom network, it will be difficult to achieve a 
country-wide growth powered by IT. Secondly, SMB is an important segment in Oman 
and here, the recognition given to software and services is very low. These 
companies can achieve a lot if they spend adequately on software and services. 
This is due to lack of awareness and a drive to educate them will help.”<br>
<br>
<b>Need for IT Association</b><br>
While talking to different IT companies, OER felt that despite having concerns 
and good ideas to expand the IT market, market players are not coming together 
for a common cause. So, we posed the questions to various head honchos on the 
need for creation of a common platform and how this could take the industry 
forward. <br>
<br>
Potnis said, “There is a need to create a platform where government and private 
IT companies can come on a common platform to discuss and chalk out plans for 
promoting Oman on the international map from the IT point of view. A sustained 
common effort needs to be made. This platform will help to look at the 
opportunities and challenges Oman has from the technology perspective. Such 
involvement will create an ownership, with long-term benefits. This will also 
result in pro-active efforts towards developing, on an ongoing basis, local 
skill sets, which will go a long way in developing the nation.” <br>
<br>
Sardiwal believes “A forum facilitating closer interaction within the IT 
industry will definitely help the cause. However, operating in a small market 
puts practical limits on the extent of cooperation amongst local companies. 
Also, various IT companies are at different stage of maturity. A common 
platform, supported by large companies and governmental organisations will be 
ideal to share views and drive a long-term plan to achieve the Digital Oman 
vision articulated by ITA. This IT Forum can be used to improve technology 
awareness in the corporate sector, offer practical advice to college students, 
work with the government to conceptualise e-governance applications and bring 
international expertise to the country.”<br>
<br>
Sheila Hopper Jamal, Sr. Vice President, Al Madina Development &amp; Supply, went a 
step forward, as she mooted the idea of “all IT companies sharing and exchanging 
their expertise to build a top-class solution instead of re-inventing the 
wheel.”<br>
<br>
Mohammed Al Maskari, DG, KOM summed it up, when he said that “There will be 
weaknesses in management, expertise, opportunity and in capital, but one can 
work with all of those if one cooperates. We understand that we need to help 
each other, work together, to be successful together. The big issue is success.”</font></p>
