<b>Building A Digital Oman Society</b></font><font face="Verdana" size="1"><br>
<b>In a tête-à-tête with OER, Dr. Salim Sultan Al-Ruzaiqi, CEO, Information 
Technology Authority, outlines the efforts of Oman to become an IT enabled 
society </b> <br>
<br>
<b>
<img border="0" src="images/spotlight2.jpg" width="150" height="179" align="left" hspace="6">To start with, what are the objectives of the Information Technology 
Authority (ITA)?</b><br>
ITA was set up by a Royal Decree promulgated on May 31, 2006. We are an 
autonomous legal body affiliated with the Minister of National Economy. 
Pioneering the implementation of Digital Oman strategy is the mission of ITA. 
The strategy comprises of a wide range of initiatives and services that are 
designed and created to improve the efficiency of government services, enhance 
the activities of businesses and empower individuals with skills and knowledge 
to meet society’s needs and expectations and to direct Oman towards becoming a 
sustained knowledge-based economy.<br>
<br>
Currently, we have around 40 people and hope to grow into a 90-member 
organisation by the end of the year.<br>
<br>
<b>What is the Digital Oman Strategy?</b><br>
Oman has embarked on empowering its people, through the eOman initiative, 
besides opening up thousands of job opportunities in the IT sector. The goal 
includes a significant improvement in the quality of services the government 
provides to its citizens. <br>
<br>
In this context, the digital strategy has been prepared according to a 
collaborative and integrated methodology, with the objective of boosting the IT 
economy of the Sultanate. It includes providing e-government services to all 
Omani society sectors, with clear definition of policies for a healthy 
environment to encourage IT industry in Oman and its export. In addition, it 
aims at developing the communication sector according to international standards 
and the restructuring of the telecom sector, enabling active participation of 
private sector, thus facilitating competition.<br>
<br>
<b>How are you working to meet your objectives?</b><br>
We have a time-bound business plan to follow. We are working on multiple 
projects simultaneously. We have made an excellent progress in infrastructure 
related projects. We are launching Ubar portal, e-payment gateway and data 
centre in 2007 in different phases. The e-government centralised gateway–Ubar 
portal (named after the famed lost city of ‘Ubar’–a main trading gateway into 
the Arabian Peninsula) will be the main entry point to e-government services, 
integrating other government sites and portals. It will authenticate access and 
entry to all government services for the delivery of citizen centric services 
electronically. The internet-based payment gateway will enable citizens to make 
their payments online through multiple instruments and thus take Oman to a 
higher level of e-services and e-commerce. We expect our security framework to 
be ready this year. <br>
<br>
Secondly, we are enabling other government agencies in delivering their services 
through the Ubar portal. In the first stage, we are working closely with the 
ministries of national economy, finance, commerce and industry along with Muscat 
Municipality and Tender Board. All these five organisations have initiated some 
e-services and we are facilitating them to integrate those services with the 
Ubar portal. In the second phase, we will cover more government bodies to expand 
the offerings on the Ubar portal. <br>
<br>
In our e-services project, we are conducting a study for all government 
organisations to help them in identifying the services that they can offer 
online. So, over a period of time, all government organisations will start 
offering e-services. Many of them will be integrated with the Ubar portal, with 
more being added in phases. We have already identified most of the services, 
which can be e-enabled and are taking them up on a priority basis.<br>
<br>
Thirdly, ITA has embarked on a National IT Training &amp; Awareness project. It is 
aimed at developing ICT skills and increasing ICT awareness within the 
government and the community. On the awareness front, we are expecting to finish 
the first phase of the eOman Road Shows by February, with the final event being 
held in Sur. Subsequently, we plan to roll out our second phase and reach out to 
7-10 more locations. In the training exercise, we have launched our civil 
services training pilot programme. We aim to train more than 104,000 government 
employees across the Sultanate. Very soon, we are also going to launch a pilot 
programme for citizen’s training by introducing learning centres in 5 locations. 
We are also working very closely with the private sector to identify the skills 
we need to inculcate in our IT academic programmes to enable IT graduates to be 
productive from day one. <br>
<br>
We are working on many other projects on infrastructure, to drive citizen 
awareness and training, and to help the government in bringing services online, 
and we will share this with you as the time comes. <br>
<br>
<b>Talking of interaction with the private sector, ITA had recently organised a 
round table forum with the IT captains to discuss the Digital Oman Strategy. 
What were their recommendations? </b><br>
The forum was a good beginning to involve IT drivers from the private sector to 
promote IT in Oman. The Sultanate is one of the few countries in the region to 
introduce IT in a very strategic and scientific way. The ICT experts lauded the 
progress of the numerous eOman projects underway. <br>
<br>
This apart, we discussed several areas, including adoption of technology, 
delivery of public services, infrastructure, capacity building related to ICT. 
Several thoughts and suggestions emerged from the discussions. A lot of emphasis 
was made on the creation of local Arabic content, which could create a Gulf-wide 
market. The forum also focused on challenges in the adoption of online 
government services both by businesses and by citizens.<br>
<br>
Sustainable e-government requires development of support infrastructure and 
increased telecommunication connectivity and access, and this emerged as a key 
focus area through the forum.<br>
<br>
We hope to have such interactions at least once a year as part of our 
private-public partnership efforts. Through such programmes, the private sector 
also gets to know about what we are doing and they can plan accordingly. <br>
<br>
<b>Is there any association of IT companies in Oman?</b><br>
As far as I know, the OCCI has a committee on ICT. We need to see how we can 
work together. A focus group of ICT professional has applied for the creation of 
an ICT society of Oman. This is an important milestone in the eOman strategy and 
we hope to get it approved by the concerned authorities soon. This society, 
comprising of ICT professionals from all sectors, will actively support the 
implementation of the Digital Oman strategy. <br>
<br>
<b>
What barriers you face in your work? Do you think enough investments are not 
being pumped into IT?</b><br>
We have started many projects simultaneously. We have set a goal for ourselves 
that Oman needs to reach a particular level in the coming year. It’s quite a 
challenge. Investment is not an issue, as the government believes that IT has an 
important role to play in the socio-economic development of the country. <br>
<br>
<b>What is your annual budget?</b><br>
The budget for Digital Society is RO70 million for the period 2006-2010. But 
overall investment in the IT sector in Oman is much higher, as every 
organisation has its own budget for IT operations. <br>
<br>
<b>Do you think there is a need to improve internet services within Oman?</b><br>
I do agree that we have to cover the whole country with reliable internet access 
and we are working very closely with TRA and Omantel in this direction. I am 
positive that while everyone faces problems, we must jointly work towards 
solving them.<br>
<br>
I believe that all of us have to do our part and work very hard to turn our 
dream of Digital Oman into 
a reality.</font></p>
