<font face="Verdana" size="4">Driving Investment</font></b><font face="Verdana" size="1"><br>
<br>
<b>HE Ahmed bin Abdulnabi Macki, Minister of National Economy and Deputy Chairman 
for Financial Affairs and Energy Resources Council, on Oman’s economic 
performance in 2006 and general budget for fiscal 2007</b><br>
<br>
<img border="0" src="coverstory.jpg" width="185" height="217" align="left" hspace="5" vspace="5">During 2006, the global economy has been characterised by sound performance and 
is expected to achieve a growth rate of 5.1 per cent according to the forecast 
of the International Monetary Fund (IMF). This is due to the decline in energy 
prices on the one hand and strong demand on the other. <br>
<br>
However, in 2007, the growth rate of the international economy is expected to 
decrease to 4.9 per cent. Such decline is attributable to the increasing current 
account deficit in the US and the expected rise in energy prices in addition to 
the increase in the cost of borrowing. However, the growth rate of the 
international groups are expected to vary due to the decline in growth rate of 
advanced countries to about 2.5 per cent due to the impact of the US economy’s 
performance, some countries like China and India are expected to continue 
recording high growth rates ranging between 9-10 per cent. <br>
<br>
International markets’ forecast indicate that oil prices will remain stable in 
2007 at their current levels. The forecasts are based on the assumption of 
stable geopolitical situations and moderate weather as well as the assumption 
that OPEC intervention will ensure balance between supply and demand.<br>
<br>
<b>Economic Performance in 2006<br>
<img border="0" src="coverstorya.jpg" width="204" height="319" align="right" hspace="5" vspace="5"></b>Preliminary estimates of GDP indicate that the national economy will achieve 
a growth rate of 16.8 per cent continuing its characteristic in creasing growth 
in recent years. Such performance is due to the increase in oil prices and an 
appropriate climate that is conducive to growth facilitated by the economic and 
monetary policies followed by the government. In addition, it was due to the 
noticeable expansion in LNG industry, the petrochemical industries, strong local 
demand together with improvement in non-oil exports. <br>
<br>
One of the positive features of the national economy’s performance in 2006 was 
that all the main economic activities contributed to this growth. The value 
added for oil activities, in spite of the decline in oil production by 5 per 
cent relative to 2005, it will grow by the rate of 17.9 per cent as result of 
increase in oil prices and increase demand for natural gas. However, non-oil 
activities are expected to grow by 15.6 per cent, which is strengthened by 
actual growth rate in both LNG industry and tourism sector by 60.7 per cent and 
22.4 per cent, respectively. <br>
<br>
Regarding foreign trade, the expectations indicate an increase in the gross 
proceeds of exports in 2006 by 15.4 per cent compared with 2005. The oil exports 
are expected to grow by 8.2 per cent and non-oil exports of Omani origin are 
expected to increase by 11.4 per cent by virtue of increase in the exported 
quantities of LNG, both in value and quantitative terms, as also the initiation 
of fertiliser exports. The proceeds from re-export operations are expected to 
increase by 21.6 per cent.<br>
<br>
The imports are expected to increase in 2006 relative to 2005 by 15.5 per cent 
as a result of the increase in commodity prices in international markets and 
decline in the exchange rate of the US dollar relative to other foreign 
currencies. This will result in an increase in the surplus of balance of trade 
in 2006 by 15.4 per cent compared with 2005.<br>
<br>
Regarding Muscat Securities Market, the trading volume during 2006 reached RO1.1 
billion, representing 8 per cent of the Sultanate’s GDP. The positive 
performance of the national economy in 2006 was reflected positively on the 
performance of the market whereas its general indicator increased by 14 per 
cent.<br>
<br>
The fiscal and monetary policies, regulations and laws organising the security 
market had a positive impact on its performance and contributed actively to 
overcome the obstacles that markets in neighbouring countries face.<br>
<br>
Continuance to the good performance of the national economy during 2006, it is 
expected that it continues its growth rate at all the economic sectors and 
activities levels particularly the non-oil activities which are expected to 
achieve a growth of 12.5 per cent.<br>
<br>
In the domain of the approved development programme for civil ministries in the 
Seventh Five-Year Development Plan, which is considered the main impetus for the 
plan and its executive tool, the actual approbations for the programme amounted 
to RO3,016 million. During 2006, a number of projects were added in response to 
Royal Directives issued during the annual Royal Tours, which necessitate the 
perception of the urgent needs of citizens in wilayats and regions, and work 
towards their provision, together with many of infrastructure projects.<br>
<br>
The total of these additions reached RO612 million and thus the approbations for 
the development programme increased by 20.3 per cent to reach RO3,628.3 million. 
Of this RO364.1 million is the cost of new projects planned to be implemented 
during this year. Following is a summary of these additions at the sectoral 
level:<br>
