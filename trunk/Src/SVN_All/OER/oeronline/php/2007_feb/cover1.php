<font face="Verdana" size="1"><b>Deficit<br>
</b>Based on the approved estimates of revenue and expenditure of the public 
budget, the deficit for 2007, on oil prices being assumed to be US$40, is 
estimated to be about RO400 million, i.e a ratio of 9 per cent of revenue and 3 
per cent of the GDP. This is lower than the deficit estimated for the 2006 
budget by 38 per cent. It is also considered relatively low regarding both 
revenue or GDP. It is planned that the deficit to be covered by withdrawal from 
emergency reserve allocation. <br>
<br>
Lastly, it is worth mentioning that the growth rate in the general finance for 
this year at 15 per cent reflects the keenness of the government to encourage 
and promote the national economy to continue to maintain the growth rate 
realised in the recent years and also to promote comprehensive development in 
various sectors as well as expansion of government services so that it reflects 
on the living standard of the people and helps achieve prosperity of society.
<br>
