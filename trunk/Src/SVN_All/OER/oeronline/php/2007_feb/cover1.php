<font face="Verdana" size="1"><b>Public Finance</b><br>
The state general budget for 2006 was approved as follows:<br>
<br>
<img border="0" src="button.jpg" width="5" height="4">
Total Revenue – RO3,587 million<br>
<img border="0" src="button.jpg" width="5" height="4">
Total Expenditure – RO4,237 million<br>
<img border="0" src="button.jpg" width="5" height="4">
Deficit – RO650 million<br>
<br>
The oil prices, for budget purposes, were assumed to be US$32 per barrel in 
spite of the additional; projects approved during the year and the finance 
approbations which were allocated to cover the different aspects of public 
expenditure, in spite of this and though by virtue of the stability witnessed in 
the international prices of oil, it is expected that the actual budget will 
realise a financial surplus after deficit coverage in the range of RO2,400 
million. This will be used for the promotion of the government fiscal reserves 
and payment of receivables of the retirement funds.<br>
<br>
<b>Budget 2007: Revenue &amp; Expenditure</b><br>
The state public revenue for fiscal 2007 is estimated to be about RO4,490 
million as compared to RO3,587 million for fiscal 2006, an increase of RO903 
million or 25 per cent. Oil and gas revenues represent 79 per cent of the 
overall revenue while current and capital revenue represent 21 per cent. </font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="coverstory3.jpg" width="447" height="230"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">The oil revenue was estimated at an average price of US$40 per barrel and a 
daily production rate of 730 barrels. Based on these assumptions, it is expected 
that the oil revenue reach about RO3015 million and its contribution to the 
overall revenue will be 67 per cent. The gas revenue will total RO550 million or 
12 per cent of overall revenue. The current and capital revenue are estimated at 
RO925 million, an increase of 37 per cent from that of 2006 budget.<br>
<br>
The overall public expenditure for the fiscal year 2007 is estimated to be about 
RO4,890 million relative to RO4,237 million for fiscal 2006, an increase of 
RO653 million or 15 per cent. This is to cover the following expenditure 
elements and components:<br>
<br>
<img border="0" src="button.jpg" width="5" height="4">
The current expenditure estimated for ministries and units of the civil 
government is about RO1,620 million, an increase of RO220 million or 16 per cent 
from last year’s estimates. The current expenditure represents 33 per cent of 
the overall public expenditure. The allocations include coverage of basic 
government services and operating expenditure for government ministries and 
units, coverage of growth in education and health services, and operating 
expenditure for new projects. It is worth mentioning here that the share of 
education and health constitutes 50 per cent of the gross current expenditure.
<br>
<br>
<img border="0" src="button.jpg" width="5" height="4">
The oil and gas production expenditure is estimated to be RO1,205 million 
representing 25 per cent of the gross public expenditure, i.e. an increase of 32 
per cent from the approved budget of 2006.<br>
<br>
<img border="0" src="button.jpg" width="5" height="4">
The allocations for the development budget are estimated to be RO500 million, an 
increase of 33 per cent compared with the approved budget for 2006. This is to 
cover the expenditure of ongoing development projects in different sectors which 
are included in the Seventh Five Year Plan (2006-2010).<br>
<br>
<img border="0" src="button.jpg" width="5" height="4">
The support allocated for the electricity sector is RO135 million, an increase 
of 7 per cent from the approved budget for 2006.</font></p>
