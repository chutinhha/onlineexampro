</font><b><font face="Verdana" size="4">Soaring High</font></b><font face="Verdana" size="1"><br>
<b>
<img border="0" src="spotlight7.jpg" width="300" height="164" align="right" hspace="5" vspace="5">With focus on tourism and booming economy, the aviation business in Oman is on 
an upswing. Akshay Bhatnagar reports.</b><br>
<br>
The global commercial aviation business is under tremendous pressure. Even 
though revenue has gone up from US$329 billion in 2000 to an estimated US$450 
billion in 2006 (source: IATA), net profit has gone down drastically. The 
industry last registered a net profit in the year 2000. Thereafter, it has 
registered constant losses mainly due to a slump in the US market and higher oil 
prices. In 2006, as per IATA estimates, the global aviation industry reported a 
net loss of 1.7 per cent.<br>
<br>
Contrary to global trends, the Middle East market recorded a net profit of 0.2 
per cent per annum for the period 2004-2006 (source: IATA). The ME
market holds the maximum potential for passenger and cargo traffic growth. The 
international passenger traffic in the ME region is expected to have grown by 
9.5 per cent in 2006 and is expected to continue growing at the rate of 6.9 per 
cent in the period 2006-2010, more than any other market across the world. (See 
Table: Traffic Forecast By Region.)<br>
<br>
<b>Oman – Strategic Development</b><br>
The Sultanate of Oman is among the major catalysts in the growth of aviation 
business in the Middle East. Oman, with a major thrust on tourism development, 
is emerging as an important traffic hub on the global aviation map. On the 
infrastructure front, the government of Oman is modernising its existing 
airports besides building new ones to meet the growing demand. The national 
carriers, Oman Air and Gulf Air, connect the Sultanate to most of the important 
cities in the world and with the expansion of their fleet are continuously 
introducing new routes. <br>
<br>
Major international airlines along with regional carriers and budget airlines 
are also increasing their connecting flights to Oman. Air travellers to and from 
Oman, both in the leisure and business segments, now have a number of flying 
options to choose from. This has also led to more scope for growth in exports 
and imports to boost trade in Oman. “Oman has assumed significant strategic 
importance on the regional and global aviation map. Geographically, Oman is 
strategically located, being the entry point from the Indian subcontinent into 
the Gulf,” says Ziad bin Karim Al Haremi, CEO, Oman Air. (See Steady Progress.)<br>
<br>
Mansoor Mushabech, Emirates’ Area Manager for Oman, adds, “The Oman government’s 
initiative and commitment towards supporting the tourism industry has ensured 
that the country is well connected to all key gateways and its international 
arrivals are on an accelerated growth path.”<br>
<br>
No wonder, air traffic movement at Seeb International Airport has gone up 
substantially. In the period January-October 2006, 17,368 international flights 
landed at Seeb (an increase of 17.1 per cent over the same period in 2005). 
Similarly, 17,346 flights took off, which again was 17 per cent higher than the 
corresponding period in 2005. In terms of international passengers, 1.75 million 
air travellers arrived at Seeb, which was 37 per cent higher than the 
corresponding figure in 2005. And 1.73 million passengers departed from Seeb, 
35.1 per cent more than the 2005 figures <br>
(Source: Ministry of National Economy). </font></p>
