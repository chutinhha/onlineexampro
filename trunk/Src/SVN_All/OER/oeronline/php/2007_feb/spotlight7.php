<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
Sharjah-based Air Arabia, Middle East’s first low cost carrier which is coming 
out with an IPO in the first quarter of 2007, launched its first flight from 
Muscat on October 29, 2003. Godfrey Mascarenhas, Manager, Air Arabia – Oman, 
says “We started with a thrice-weekly frequency and from April 2007 will operate 
10 flights a week to Sharjah–5 morning departures and 5 evening departures for 
maximum customer benefit. This increase has been necessitated by the steadily 
increasing demand for cost effective and safe travel.” Around the region, Air 
Arabia flies to all GCC points and Teheran. In Africa and the Middle East, it 
covers Alexandria, Luxor, Assiut, Damascus, Aleppo, Latakia, Khartoum, Amman, 
Beirut and Sana. The airline covers India with daily flights to Mumbai, Kochi, 
Thiruvananthapuram, Chennai, and Jaipur apart from four flights a week
to Nagpur. In the Asian sub-continent, Colombo, Kabul, Kathmandu, Almaty, Astana 
and Yerevan are the other destinations that it connects from Oman and in Europe 
it connects Istanbul.<br>
<br>
Talking about the airline’s Oman operations, Peter Schilter, Manager – Oman, 
Swiss say, “Muscat, with daily frequencies from Zurich via Dubai, is a smaller 
destination in the Swiss network. However, thanks to the high seat load factor 
throughout the year and excellent share of First and Business class travellers, 
it is amongst the most profitable routes for the airline. Since the introduction 
of daily flights, Swiss has more than doubled the number of passengers between 
Switzerland and Oman. In 2006, we carried around 35,000 passengers, a 20 per 
cent increase over 2005.”</font></p>
