<b>National Carriers – Connecting to the World</b><br>
The major credit for the growth of aviation in Oman goes to its two national 
carriers, Oman Air and Gulf Air. Both have led to unprecedented inward and 
outward traffic in Oman by connecting the Sultanate to major centres globally 
and created huge opportunities for organisations and individuals to upscale 
their activities related to the country.</font></p>
