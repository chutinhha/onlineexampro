<p align="justify" dir="ltr"><font face="Verdana" size="1">Gulf Air has named Swiss national Andre Dose as its Chief Executive Officer. He 
replaces James Hogan, who left last September and now heads Etihad Airways. Dose 
is a former CEO of European regional airline, Crossair and Switzerland’s 
national carrier Swiss. It will be interesting to see how Dose spearheads Gulf 
Air and what role he envisages for Oman in the new three-year strategic plan for 
the airline.<br>
<br>
<b>Regional &amp; International Carriers in Oman </b> <br>
Regional and international airlines have also increased their operations from 
Oman to take advantage of the growing business opportunities. Khondaker Kabir, 
Sales Manager – Oman, Etihad Airways, says, “Etihad launched its much-heralded 
daily service between Muscat and Abu Dhabi in March 2006, with a brand-new 
purpose built two-zone A330-200 aircraft. A year later, traffic figures on this 
route have increased three-fold, with a significant number of Pearl and Coral 
Zone guests making the short flight between the two capital cities.” He adds, 
“The Omani market has seen consistent growth during the past year with a 
significant increase in demand from both business and leisure travel, which 
formed a key part of Etihad’s business plan. As the Abu Dhabi-Muscat sector 
continues to grow and more visitors look to come to the Sultanate, Etihad will 
look to build on these services.”<br>
<br>
Emirates operate two daily flights between Muscat and Dubai. It also has 32 code 
share flights a week with Oman Air to Dubai. Mansoor Mushabech, Emirates’ Area 
Manager for Oman says, “Oman is an important market for Emirates. The airline 
has played a catalytic role in boosting inbound business and tourist traffic 
from Europe, Asia and the United States into Oman. Emirates connects Omani 
travellers and shippers with a 87-city strong global network via its Dubai hub. 
Germany and United Kingdom in Europe; Australia, China, Thailand, Japan and 
Korea in Australasia; South Africa, Morocco and Egypt in Africa, and Dubai are 
some of the popular destinations for Omani travellers.” Emirates plan to expand 
its business in Oman by enhancing its global network and offering new products 
and services. Emirates is also expected to strengthen its route network with the 
addition of several new gateways like Venice, Newcastle and Sao Paulo, and 
increase flight frequency and capacity on high-demand routes.<br>
<br>
Qatar Airways, one of the fastest growing airlines in the world, plans to 
operate two daily flights from Muscat from this summer. What makes the airlines 
so bullish about the Oman market? Ali Abdul Khaliq, Regional Manager-Arabian 
Peninsula, Qatar Airways-Oman, responds, “The new international airport coming 
up at Sohar will boost the aviation industry in Oman. Rapid growth in tourism in 
Salalah will encourage more international airlines to include it in their 
network. Qatar Airways-Oman will also extend its customer services by setting up 
a dedicated office in Salalah.” Qatar Airways connects more than 71 destinations 
worldwide. Its network includes Europe, Africa, Far East, Middle East, Indian 
subcontinent, GCC and by mid summer, it plans to expand its network to the US. 
Ali adds, “Qatar Airways aims to grow and add more new destinations connecting 
out of Oman.”</font></p>
