</font><font face="Verdana" size="4">
<b>Moving up, Surely</b></font><font face="Verdana" size="1"><br>
<b>The budget confidently outlines steps that will maintain Oman’s growth momentum. 
Greater fiscal spending will translate into derived demand and greater 
purchasing power, writes Dr. Jayaram Nayar<br>
</b>
<br>
This is a budget that will maintain the growth momentum. On the back of a strong 
anticipated 2006 growth rate of 16. 8 per cent, the budget attempts to build on 
the growth of non-oil sector estimated at 15.6 per cent in 2006. This is in 
keeping with the recognition that because the oil sector’s potential may well 
have been tapped, the medium term strategy should be on high rate of growth in 
the non-oil sector. The budget has for its base, persistent growth, financial 
stability, confidence in the economy, and a stable currency. <br>
<br>
The public revenue estimated in 2007 to be RO4,490 shows an increase of RO903 
million over the 2006 figure. This in itself is going to be representative of 
liquidity and dynamism in the system. The deficit of RO400 million on the 
assumption of US$40 per barrel is indicative of prudence and conservatism. Given 
the sustained high prices, this may well be nominal. The growth of the global 
economy at 5.1 per cent in 2006 is an indicator of the demand factor for fuel. 
Nevertheless, the deficit is bound to trigger enhanced aggregate demand and thus 
stimulate further growth.<br>
<br>
The objectives of Omanisation and diversification of the economy are at the 
heart of the increased allocations to the education sector. The emphasis on 
human capital qualitative improvement is seen in the allocation of RO609 million 
to this sector. The budget thrust on upgrading skills of Omanis is borne out of 
the contribution human capital can make to growth. Assigning 38 per cent of the 
overall expenditure to education is a significant statement step to investing in 
the future. It is now for the beneficiaries to respond to this gesture. The 
budget is clearly in response to global competition and the quest for 
efficiency. <br>
<br>
The budget seeks to keep up the pace of 2006 spending on infrastructural 
facilities. The allocations for the development budget at RO500 million and for 
the electricity sector at RO135 million are bound to accelerate the momentum of 
derived spending. In Oman, as we all know, governmental spending facilitates 
private spending. <br>
<br>
<img border="0" src="coverstory8.jpg" width="204" height="356" align="left" hspace="8" vspace="8">The services sector is also likely to get a fillip. Banks already flush with 
liquidity are bound to be beneficiaries of both the higher revenue garnering and 
the higher outlays on expenditure. This is so as they are the routing agents for 
receipts and payments in the system. Bank customers would benefit as banks seek 
avenues to deploy liquidity through lending. However, a word of caution: there 
is a possibility of adverse selection among borrowers as there is competitive 
lending. Banks ought to be careful that expanded liquidity does not enhance the 
percentage of non-performing assets at a later date. Given the fact that banks 
have also been active in the recent spurt in industrial activity in Oman, bank 
lending to corporate entities will only increase and this is likely to see a 
greater sophistication of the system. The high level of government spending also 
lowers the vulnerability of the corporate sector and improves market/investor 
confidence. Given the track record of optimal fiscal management, market players 
would be beneficiaries. The increased revenue from taxes at RO132 million and 
customs duties at RO80 million reflects the state of optimism in the economy.<br>
<br>
One of the worrisome factors in the GCC countries is the possible rise in 
prices. Given that liquidity is high and the sustained fall in dollar, the 
budget rightly recognises that inflation has to receive prioritised attention. 
Inflation control is possible through effective coordination between fiscal and 
monetary policies. The earmarking of RO138.5 million towards housing is a 
stimulating infusion: as it has considerable derived demand element. <br>
<br>
Housing starts are a good indicator of buoyancy in the economy. Constructional 
activities are bound to have multiplier effects. The mismatch between rental 
houses demand and supply is a good indicator of growth patterns abut also 
escalates inflation. <br>
<br>
One sector which has been the beneficiary of strong fiscal spending and 
consequential consumer spending has been Oman’s automobile sector as the 
spending percolates into greater purchasing power for individuals. This sector 
is likely to do even better in 2007. With pay scales moving up in the budget, 
enhanced purchasing power among consumers is likely to stimulate spending on 
consumer durables. <br>
<br>
