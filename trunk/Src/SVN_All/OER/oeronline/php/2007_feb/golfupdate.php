<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Teeing off 
more than a game</font><font face="Verdana" size="1"><br>
PGA top brass dwell on how the multi-billion golf industry is growing all over 
the world<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="golf.jpg" width="250" height="169" align="left" hspace="5" vspace="5">“It’s common in every country, of every 
economic size and influence, that the PGA Professional introduces the next 
generation of customers to the game,” said Joe Steranka, CEO of PGA of America. 
“Not only do PGA Professionals give advice that improves player’s performance, 
ultimately increasing the enjoyment and excitement for amateurs, but they also 
have a significant impact on expanding the multi-billion dollar golf industry.”<br>
<br>
Steranka made his remarks at the inaugural PGA Global Economic Summit. With him 
were Chief Executive Sandy Jones of PGA of Great Britain and Australian CEO Max 
Garske. The trio presented before an audience of over 250 participants research 
that underscored the extent of the economic impact of the business of golf. 
According to them, the Asia-Pacific region is the recognised home of the ‘Golf 
Boom’ taking place in the early part of the 21st century. There are currently 
6,000 golf courses, with an excess of six million golfers. China is registering 
a 40 per cent growth rate in golf course construction and anticipated to 
challenge the golf economies of Europe and the US in the next decade.<br>
<br>
The US golf economy totals US$62.2 billion annually, Steranka reported, with 25 
million golfers spending an average of $2,400 per person every year. A typical 
course hosts an estimated 33,300 rounds, employing an average of 17 persons on a 
full-time basis. He said the Stanford Research Institute is again conducting a 
study on the US golf economy based on available government data for the year 
2005, the report of which will be out next year.<br>
<br>
Europe’s golf economy features more than 6,300 golf courses that contribute an 
estimated $19.9 billion to the region’s economy. It has an estimated 4.1 million 
golfers, with an average yearly spending habit of $4,846 per person. The 
Australian Golf Industry Economic Report reveals that the country’s golf economy 
totalled $US2.2 billion, and is home to 1.25 million golfers who play 1,700 
different courses. A typical Australian golfer spends an average of $1,700 per 
year, and there are nearly 23,000 jobs created for every 100 golfers.<br>
<br>
“One of the ironies of golf in established areas of the world,” said Garske, “is 
the sense of complacency. Where golf is well established is where the game is 
struggling. In Australia, we never go out and have a social game. We always have 
a competition. Any social golf is played at public courses and that is something 
that we have to change along with the idea that the sport will take care of 
itself.<br>
