		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><font face="Verdana"><b><font style="font-size: 14pt">Channel 
        Challenge<br>
        </font></b></font><b><font face="Verdana" size="1">Hugh Miles delves 
        into the makings and workings of Al-Jazeera, a TV channel that inspires 
        love and hate</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="images/browsing.jpg" width="150" height="245" align="left" hspace="6" vspace="5">In the rush to catch my flight back to Muscat a few
weeks ago, I rummaged through the bookshop at
Dubai airport and picked up Hugh Miles’ maiden
book, Al-Jazeera: How Arab TV News Challenged
The World, chronicling the path-breaking success
of the world’s most controversial and most watched
television channel Al-Jazeera. I have no regrets. A
pleasant intellectual journey it was.
<br>
<br>
Remove Al-Jazeera for a second and one can
almost obliterate the image of Al Qaeda and its chief
protagonist Osama Bin Laden. What is considered
to be inflammatory and untouchable in the sanitv
tised media world is the main course of dinner for
the Doha-headquartered Arabic TV channel that is
shaking the entire world, literally.
<br>
<br>
No wonder, Al-Jazeera is everyone’s whipping boy.
The United States hates its guts for offering “opinion
and the other opinion” (italics mine). For Saudi Arabv
bia, Al-Jazeera is persona non grata – and if Miles
is to be believed, the Saudi kingdom is leaving no
stone unturned to kill the Arabic television promoted
by the Emir of Qatar commercially. Even a section of
Palestinians, whose cause the channel is believed to
be championing, is piqued. Iraq, on its part nurses a
love-hate relationship.
<br>
<br>
Barring the Arabic-speaking watchers across the
globe who are lapping it up, Al-Jazeera, by and
large, has a huge friends’ deficit. The only exceptv
tion is the Emir of Qatar who is bankrolling the ‘adv
venture in communication” at a historically crucial
time, when the world is split into two, which has
been exquisitely captured by American scholar
Samuel P. Huntington in his now-classic and mostthumbed-
through The Clash of Civilizations.
<br>
<br>
From Miles’ account, two sets of people emerge,
whose guts and passion to get the channel going
needs three rounds of applause. Primarily, the Emir of Qatar for his refusal to bow down to pressure from
the likes of global superpower – read the US – and
powerful and large neighbours such as Saudi Arabia.
When US President George W Bush is credited with
the idea of bombing down the channel to silence
it once for all, one will understand the power and
might of the channel. The problem, as perceived by
its detractors, is that it exposes the hypocrisy of the
mighty – and nobody wants to be found naked.
<br>
<br>
The Al-Jazeera team is the other entity to whom
applause is due. Their resilience, in the face of
physical and other forms of attack from all quarters
to continue what they believe in doing unflinchingly,
is remarkable. Such devotion to capture and present
news to the neighbourhood in the first place, and
the world, can come about only if the promoters
throw their weight behind these crusaders. Luckily,
the Emir is not found wanting.
<br>
<br>
Al-Jazeera’s short history is incredible. Miles captures
the origin, trials and tribulations of the channel
in the past couple of years succinctly in beautiful
prose. Nevertheless, the thoroughly researched and
documented book is not flawless. One cannot escape
feeling that it is a public relations assignment given
to a rookie. Of course, Miles has provided space for
Al-Jazeera baiters. Sort of balancing act. Yet, it is not
difficult to decipher where Miles’ sympathies lie.
After all, show me a scribe who does not champion
Davids pitted against Goliaths?
<br>
<br>
The book is repetitive and laborious in many places.
The author could have easily done away with at
least 100 odd pages and still retained the reader’s
loyalty. All said and done, the birth of Al-Jazeera –
(another name for fearless journalism?) – is the most
desirable happening for the Middle East. Any book
that chronicles this transformation on the media firmv
mament is to be welcomed.
<br>
<br>
<b>Ramesh Kumar</b></font></p></td>
