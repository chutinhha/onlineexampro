<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Though Oman easily met the initial entry criteria, Ahmed bin Abdulnabi Macki, 
the Minister of National Economy, confirmed in January the country’s withdrawal 
from the GCC monetary union. He affirmed that Oman had reservations over the 
lack of progress made on obtaining pre-requisites for successful union by 2010 
(no agreed regional headquarters or common market), and also cited Oman’s 
aversion to surrendering economic sovereignty. The Minister stated, “The 
Sultanate has its own economic and financial compulsions, which do not offer 
room for meeting the criteria set for the single GCC currency.”<br>
<br>
<b>Domestic Focus</b><br>
Oman has always distinguished itself from other GCC countries, both in its 
approach to oil and industry, and how it has marketed itself in the tourism 
sector. The country’s leadership is aware that it does not have the same depth 
of oil and gas reserves that most other GCC states have and the country’s 
economic diversification efforts could perhaps lose market competitiveness under 
a unified regional currency. Tourists may also well have second thoughts on the 
Sultanate as a high-end holiday destination, should they suddenly be getting 
less for exchange into Gulf dinars than previously with the Omani riyal.<br>
<br>
Interestingly, Bahrain, the other GCC state with weak reserves, is also now 
beginning to voice its own concerns over the monetary union. Common themes of 
discontent include a lack of preparedness and the incomplete implementation of a 
common economic market between member-states.<br>
<br>
Oman’s voice speaks loudly and confidently of independent economic 
considerations and this is also reflected by its independent partnerships 
outside of the regional bloc. Recent bilateral developments have thrust Oman 
onto a new platform for trade and investment. Whilst this may have not been well 
received by some GCC member-states, it has opened up a wealth of opportunity for 
the Sultanate.<br>
<br>
The Oman-US FTA has so far generated huge bilateral trade returns for Oman (45 
per cent increase in export revenues over the last 12 months). This agreement 
also gives Oman unrestricted access to the US market, and eliminates the 5 per 
cent tariffs previously in place. This is particularly beneficial as the US is 
Oman’s fourth largest import partner, responsible for US$538.7 million worth of 
imported goods in 2005, according to the annual report of the CBO. <br>
<br>
The so-called “FTA effect” is evident across the region, with Bahrain also 
entering bilateral agreements earlier last year. FTA countries in the MENA 
region have experienced an average 33.5 per cent increase in trade with the US 
during 2005-2006. However, revenue is not the only factor to be considered in 
Oman’s case, as strategic partnerships with the US stand to benefit 
diversification options in the country.<br>
<br>
Here, Oman’s intra-regional trade ties should not be forgotten. Oman has 
significant intra-regional trade with GCC countries, accounting for 18 per cent 
of total imports and 10.7 per cent of total exports. Oman is still a key part of 
the GCC market area, with the UAE being Oman’s fourth largest trading partner. 
Should the monetary union continue without Omani membership, the Sultanate may 
well find itself facing higher transaction costs to deal with the Gulf dinar. 
However, with all GCC currencies still pegged to the US dollar, this is not 
likely to represent a major issue.<br>
<br>
<b>Global Alignments</b><br>
Oman has managed to create a new sphere of influence for itself in global trade, 
establishing bilateral agreements and partnerships, fulfilling its development 
criteria promptly and efficiently. Comparatively, the GCC have repeatedly 
prolonged negotiations to forge US-GCC arrangements and have encountered many 
points of contention in aligning their independent economic policies. Bahrain 
was the first to observe that an independent arrangement with the US might 
better reflect the demands of its national economy, a concern reiterated by 
Oman’s FTA as also by the differing priorities of the GCC members. <br>
<br>
Oman also seems to be bearing its Asian priorities in mind, since its three 
major trading partners are Japan, China and Korea, who account for a collective 
44.5 per cent of total trade (21.5 per cent of imports and 58.0 per cent of 
exports). This is a regional alliance that Oman has successfully enticed and 
does not wish to lose. Sinopec is the Sultanate’s largest exporter of crude oil 
(30 per cent) and has recently announced a proposal to increase term purchases 
by up to 20 per cent for this year. China is also bidding on industrial 
contracts in the country and is seemingly paving the way for a long relationship 
with Oman. <br>
<br>
Asian loans have proved crucial to financing government projects: the Japan Bank 
for International Cooperation (JBIC) loaned $150 million to Oman as contribution 
to financing part of the project of the second phase of Sohar port, financing 
construction as well as infrastructure. Japan also participates in a “human 
resources transfer” programme, dispatching ‘experts’ in response to requests 
made by the government. The Omani-Asian links continue to strengthen and Oman is 
making care to ensure that this partnership does not become neglected at the 
expense of regional economic cooperation.<br>
<br>
<b>Task for GCC </b><br>
The GCC priority is in establishing a trade agreement with the EU, something 
that is moving very slowly since talks began in 1990. Despite the EU being 
Oman’s fifth largest trading partner, the essential composition of the GCC 
presents many institutional barriers to trade alliances, and Oman’s branching 
out may indicate its lack of willingness to keep on waiting.<br>
<br>
Although some may criticise Oman’s decision to withdraw from the monetary union 
process, the move may come to be seen as a very sensible one given the 
Sultanate’s economic position. Oman has sent a clear message to the GCC that it 
will not marginalise its domestic concerns at the expense of regional unity. 
This decision has already encouraged GCC scrutiny of what many now agree are 
unrealistic deadlines and criteria.<br>
<br>
Oman is not trying to be a rogue state within the GCC and has maintained its 
alignment with the UAE for simultaneously introducing VAT. The Sultanate can now 
concentrate on its links with the US and Asia, increasing its competitiveness at 
a time when the country will benefit from foreign investment and partnerships. 
And perhaps, through the Omani move, other GCC states may take a long look at 
the concept of monetary union and begin to outline and implement more realisable 
plans.<br>
