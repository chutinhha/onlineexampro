			<p align="justify"><font face="Verdana" size="1">Oman’s decision to opt out of the proposed GCC monetary union may well have come 
as a blow to efforts by GCC states to strengthen the economic union between 
them. However, the decision was less based on a rejection of the scheme, and is 
rather a reassertion of Oman’s need to follow a different path of economic 
development that may even see it rejoin the union if and when a more solid set 
of objectives is implemented by the member-states. It may also serve as a 
valuable wake-up call for the GCC that it needs to begin implementing what it 
says it seeks to achieve.<br>
<br>
With the most conservative GDP growth among the GCC member-states, Oman seemed 
certain to benefit from the much-vaunted ‘catch-up effect’ of the monetary 
union, most explicitly evinced in the European Union. Neither was Oman 
struggling to meet the negotiated requirements in terms of fiscal policy – 
budget deficits needed to be capped at 3 per cent, whereas Oman had fiscal 
surplus in 2005 of over 11 per cent; public debt was way below the decided limit 
and Oman’s foreign exchange reserves would easily last beyond the four months 
agreed by the GCC at earlier policy meetings.<br>
