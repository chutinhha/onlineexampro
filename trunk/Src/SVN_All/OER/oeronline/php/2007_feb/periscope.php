<p align="justify" style="margin-right: 10px; margin-left:10; margin-top:5; margin-bottom:5">
<font face="Verdana" size="1">
<br>
<b>No bull in sight</b><br>
So, it is premature to expect any immediate sustainable expansion in market 
multiples, as some GCC brokers predict in 2007 strategy reports. On the 
contrary, I argue that new bull markets are only possible if geopolitical risks 
decline, dollar rates do not rise, bellwether and blue chips exhibit strong 
earnings, oil prices do not fall below US$60 and banks do not reduce leverage 
for equities investors. Since this rosy scenario makes little sense, I do not 
accept that valuation multiples will increase in the GCC in 2007.<br>
<br>
Therefore, I recommend that investors should err on the side of conservatism and 
only invest in those GCC companies where value-investing metrics prevail. This 
means low price–earnings, low price–book value, assured (even if modest) EPS 
growth and no credit crunch engineered by the banking system. In essence, the 
value investing strategy in the GCC argues for overweighting Oman, Bahrain and 
Kuwait in a regional portfolio.<br>
<br>
It is pointless to compare GCC markets to other emerging markets like BRIC 
because the regional markets are dominated by local retail, not offshore 
institutional investors. However, it is worth noting that all six GCC markets 
exhibit valuation multiples lower than India (22X earnings) or China (30X 
earnings) for 2007. Even a classic MENA market like Egypt exhibits a 6X book 
value valuation. This is a total reversal of traditional valuation multiples 
because Egypt never traded at a premium to GCC. It is also significant that 
while the Morgan Stanley GCC investable 100 index has fallen by 49.3 per cent in 
2006, emerging markets such as Russia, India, China, and Indonesia have surged 
in value. In fact, shares in Vietnam and Shanghai / Sherzehn surged 120 per cent 
in 2006. Therefore, while there is a bubble in BRIC and Asian shares, GCC is in 
a value play.<br>
<br>
<b>Stick with the Proven</b><br>
It is now time for GCC investors to focus on large cap, value shares that have 
no speculative dimensions. This is no time to flip IPOs and risk money in 
concept small cap shares with no proven business models. The best investment 
theme in 2007, in my opinion, lies in infrastructure, logistics, telecom and 
downstream investments. It is critical to note that even with crude prices at 
US$50, the current account surplus in the GCC can be as high as 15-20 per cent 
of the GDP. So, unlike in 1998-1999, the fall in oil prices will not trigger a 
slowdown in government spending and therefore earnings in bellwether corporates. 
On the contrary, the GCC governments expect to invest US$1 trillion in 
infrastructure in the next decade. <br>
<br>
Moreover, I believe the spike in the GCC inflation we witnessed during the 
2004-2006 petrodollar boom will boost their foreign exchange reserves, global 
assets, diversify their domestic economies and reduce public sector debt. Also, 
the unique demographics of the GCC, with some of the highest birth rates in the 
emerging markets since the 1980s, suggest that education, new home formations 
and consumer goods will remain a secular growth theme not just for 2007 but for 
the next decade.<br>
<br>
The financial markets in the GCC are now at a critical inflection point. The 
bull market meltdown in the GCC in 2006 was unique. However, this does not rule 
out future sell-offs in a trading range if earnings disappoint or geopolitical 
risk premiums rise. It is, therefore, mission critical for the GCC investor to 
risk his money in alpha (specific companies), not beta (market risk) stocks.</font></p>

