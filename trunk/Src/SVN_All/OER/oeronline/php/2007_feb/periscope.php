			<p align="justify"><font face="Verdana" size="1">The valuation compression in the GCC has been dramatic as stocks indices fell by 
60–70 per cent from their 2006 levels, particularly in Saudi Arabia, Qatar and 
the UAE. It is significant that MSM in Oman was the only Gulf stock market that 
gave a positive return in 2006. While UAE and Saudi Arabia trade on low earnings 
multiples as compared to their 2004 levels, it is a fact that there has been a 
drastic earnings slowdown across the Gulf since early 2006. <br>
<br>
Since earnings visibility is low, it is only natural that markets overreact and 
valuation multiples contract to levels that simply do not reflect fundamental 
value or the macroeconomic prospects of the GCC economies. The fall in crude oil 
prices and rise in dollar interest has also played a major part in the 
compression of P/E multiples in the Gulf as much of the 2003-2005 bull market 
was based on oil at US$60 per barrel and a 1 per cent Fed Funds rate, a macro 
combination unique in the history of global financial markets. Also, there has 
been deterioration in the geopolitical environment of the Middle East, 
particularly in Iraq, Palestine, Lebanon and Iran.<br>
