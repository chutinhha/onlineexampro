</font><font face="Verdana" size="2" color="#CC3300"><b>BankMuscat to invest in Indian 
securities firm<br>
</b></font><font face="Verdana" size="1"><br>
<img border="0" src="business_briefs.jpg" width="150" height="204" align="left" hspace="6" vspace="2">BankMuscat has entered into an agreement to acquire a 43 per cent stake in the 
holding company of the leading Mumbai based securities house Mangal Keshav 
Group. The acquisition is expected to make BankMuscat the first bank in the GCC 
region to take a stake in the Indian securities sector. On the acquisition, 
Abdul Razak Ali Issa, chief executive of BankMuscat commented: “Having a 
presence in commercial banking in India through our investment in Centurion Bank 
of Punjab, we were exploring opportunities in other segments of the financial 
services sector. Mangal Keshav would be our vehicle for exposure to the high 
growth Indian securities market.” As per Morgan Stanley research, the Indian 
equity markets are experiencing one of the strongest rallies and the trading 
volumes are expected to double to US$ 3.2 trillion in 2010 from about US$ 1.6 
trillion currently. Morgan Stanley projects the Indian brokerage business 
revenues to grow to US$ 3.9 billion by 2015. The financial due diligence on 
behalf of BankMuscat was conducted by PriceWaterhouseCoopers, India, while legal 
due diligence was done by Amarchand Mangaldas, one of India’s leading legal 
firms. The transaction is expected to be completed during the first quarter of 
2007.<br>
