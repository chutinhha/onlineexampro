<br>
BankMuscat has declared a net profit of RO60.4 million for the year ended 
December 31, 2006, as against a net profit of RO45.4 million for the previous 
year, an increase of 33 per cent. Net interest income increased by 27.5 per cent 
from RO78.1 million in 2005 to RO99.5 million in 2006. Non-interest income grew 
from RO23.3 million in 2005 to RO31.1 million in 2006, an increase of 33.5 per 
