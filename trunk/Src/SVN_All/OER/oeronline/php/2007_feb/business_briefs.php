<p align="justify" style="margin-left: 10px; margin-right: 10px">
<b><font face="Verdana" size="2" color="#CC3300">PDO Discovers New Oil Fields</font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="business_briefs5.jpg" width="125" height="145" align="left" hspace="6" vspace="3"></font></b><font face="Verdana" size="1">Petroleum Development Oman (PDO) has discovered three new oil fields. One 
field, named Budour Northeast, is located in the Birba area of south Oman. The 
other two discoveries are extensions of the Ufuq and Dafiq fields, whose 
discovery was originally announced last year.<br>
“These new fields will contribute to the sustainability of PDO’s oil 
production”, said John Malcolm, PDO Managing Director. “They have been hooked up 
rapidly to our pipeline infrastructure, adding to PDO’s production in the short 
term while delivering valuable production data for the fields’ longer-term 
development.” The Budour NE field will be appraised this year on the basis of a 
long-term production test of the discovery well and the drilling of two or three 
additional wells. But initial indications are that the field contains a 
significant quantity of good-quality oil. The discovery well revealed that the 
field, at a depth of about three kilometres, contained oil along a vertical 
distance of 67 metres. And a preliminary test showed that it could produce at a 
stable rate of 8000 barrels per day. The other two discoveries – in the north of 
the country lying at depths of about 1500 metres – are hooked up for long-term 
production testing with a view to developing them as part of the plans being 
drawn up for the earlier Ufuq and Dafiq discoveries.<br>
<br>
Dr Martin Stäuble, Exploration Director of PDO, commented: “Budour NE will join 
the Birba, Harweel and Al Noor fields as the fourth major development based on 
the south Oman intra-salt carbonate formations. It is an important discovery, 
not only because of its significant size but also because it opens several new 
avenues for exploration.”<br>
