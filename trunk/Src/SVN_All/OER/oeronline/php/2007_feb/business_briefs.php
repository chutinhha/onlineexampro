RO0.058 in 2005 to RO0.073 in 2006, which works to be a return of 73 per cent on 
a nominal value of a share. The Bank’s net loans and advances portfolio grew by 
RO463 million or 33.7 per cent to RO1,835 million as on December 31, 2006 
compared to RO1,372 million the previous year. Customer deposits increased by 
RO526 million or 40.7 per cent to RO1,817 million as on December 31, 2006 
compared to RO1,291 million as on December 31, 2005. Savings deposits increased 
by 26 per cent from RO338 million to RO426 million. The Board of Directors has 
