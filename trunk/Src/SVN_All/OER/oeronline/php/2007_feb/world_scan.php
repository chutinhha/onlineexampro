<b>US fuel consumption dips in 2006<br>
</b></font><font face="Verdana" size="1"><br>
<img border="0" src="gcc9.jpg" width="150" height="219" align="left" hspace="5" vspace="5">High fuel prices may be curbing America’s thirst for oil, or so it seems. As per 
the American Petroleum Institute, the US oil industry’s main trade group and 
lobbying arm, the country’s petroleum use fell in 2006 for the second year in a 
row. Airlines burned 2.8 per cent less jet fuel. Factories and utilities stopped 
using residual fuel oil for power and switched to natural gas. To be sure, not 
everyone consumed less. Drivers bought more gasoline and diesel in 2006 than in 
2005. But gasoline use grew just 0.8 per cent, well below the 2 percent annual 
increases seen earlier this decade. The statistics support other recent data 
showing that the soaring oil prices have had an effect – albeit an uneven one – 
on consumption.<br>
<br>
Some sectors of the economy have had an easier time adjusting than others. Much 
of American industry switched from natural gas to oil several years ago, when 
gas prices were soaring, but switched back last year when natural gas prices 
fell, said institute economist Ron Planting. Truckers, however, couldn’t stop 
delivering freight just because diesel prices jumped. “Even if the cost of fuel 
goes up, you can’t just say, ‘We’re not going to get you your groceries after 
all,’” he said.<br>
<br>
Some energy economists pin the relatively modest growth in gasoline use on 
price-shocked drivers cutting back on trips. Others say consumers are buying 
more-efficient cars. “There seems to be a trend away from the big SUVs, and not 
just for the fuel issue,” said Dan Sperling, director of UC Davis’ 
Transportation Institute. “The SUVs were the 1990s.”<br>
<br>
Interestingly, US President George W. Bush, who acknowledges a link between 
rising temperatures and mounting carbon dioxide in the atmosphere, pulled out in 
2001 from Kyoto (UN’s Protocol for fighting warming beyond 2012) under which 
most industrial nations have capped emissions.<br>
