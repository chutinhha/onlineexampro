<b><font face="Verdana" size="2" color="#CC3300">Air traffic to slow down in 2007<br>
</font></b><font face="Verdana" size="1"><br>
The International Air Transport Association’s (IATA) November traffic results 
showed that year-on-year international passenger traffic growth recovered to 6.7 
per cent, the highest growth rate recorded since May. International freight 
traffic growth for the same period remained sluggish at 3.1 per cent. Year to 
date, passenger traffic is up by 5.8 per cent and freight traffic by 4.8 per 
cent. The average passenger load factor remained strong at 73.9 per cent in 
November and is at 76.1 per cent year-to-date.<br>
<br>
“While year-to-date traffic growth is slower than the buoyant rates seen in 2004 
and 2005, it is in line with the long term industry average growth rate and has 
been a key factor behind the industry’s improving bottom line,” said Giovanni 
Bisignani, Director General and CEO of IATA.<br>
<br>
The Middle East remains the fastest growing region posting an increase of 18.3 
per cent in passenger traffic in November. However, for the second consecutive 
month, the rise in capacity (20 per cent) outstripped demand in that region. 
Africa recorded a 7.5 per cent increase followed by North America (7 per cent), 
Europe (6.1 per cent), Asia-Pacific (5.6 per cent) and Latin America (-2.4 per 
cent).<br>
<br>
International freight traffic in the Middle East rose sharply (17.3 per cent) 
followed by Africa (3.9 per cent). The Asia-Pacific (3.2 per cent) and European 
(1.4 per cent) sectors posted relatively low growth rates despite the 
improvement in underlying economic factors. “A positive revenue environment 
helped the industry reduce losses to just US$0.5 billion in 2006,” said 
Bisignani. “We expect traffic growth to slow in 2007. Airlines must continue to 
keep load factors high by carefully managing capacity and by finding further 
efficiency gains to achieve the US$2.5 billion industry profit that we are 
projecting for 2007.”</font><p align="right" style="margin: 3px 10px"><br>
