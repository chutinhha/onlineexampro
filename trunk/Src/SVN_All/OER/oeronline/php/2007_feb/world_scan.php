<b><font face="Verdana" size="2" color="#CC3300">Online fraud costs Swedish bank US$1.4 mn<br>
<br>
</font>
</b><font face="Verdana" size="1">In perhaps the biggest ever phishing attack, at least 250 customers of 
Sweden based Nordea Bank lost their savings totalling US$1.4 million. The 
hackers used a phishing email that advised bank customers to download a “spam 
fighting” programme called ‘raking.zip’ or ‘raking.exe’ that loaded what 
security companies are calling the haxdoor.ki Trojan. Obviously the phishers 
have a sense of humour in calling the software ‘raking’ – for they not only 
clearly intended raking in the cash, they succeeded in doing so to tune of over 
US$1.4 million.<br>
<br>
Haxdoor is a keylogging programme that records the keystrokes you make when you 
type in information on your keyboard, and hides itself from traditional 
anti-virus programmes. The Trojan activated itself once users visited the Nordea 
bank website, which then redirected users to a fake Nordea home page, making 
visual verification of the correct site impossible. Once the log-in details were 
entered, they were sent to US and Russian servers and then harvested.<br>
<br>
The malware waited until customers tried to log into the online banking service 
of Nordea, displaying an error message asking the customer to re-enter their 
data. Once this was done by compliant customers, the crucially sensitive login 
details were sent to the Russian hacker servers for later use in stealing funds. 
Police in Sweden have already arrested over 100 middlemen in Sweden, who it 
would seem were working with the Russian hacker criminals.<br>
