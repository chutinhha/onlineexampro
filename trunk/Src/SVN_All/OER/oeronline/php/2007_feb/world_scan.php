<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">Indonesia to invite tender for 30 blocks<br>
</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1">Indonesia is contemplating to offer 30 new oil and gas blocks this year. The 
new blocks are mostly in the deep waters of Papua and Nusa Tenggara and will 
come along with a package of incentives for investors. R. Priyono, the energy 
ministry’s director for the upstream oil and gas industry, said the blocks would 
be offered through a regular tender and direct offer mechanism, scheduled to 
start in May or June.<br>
<br>
By way of incentives, Priyono said companies could return their exploration 
rights to the government if they consider their areas unpromising after 2-3 
years of exploration.<br>
<br>
Priyono also said the government would announce in February the results of 20 
tenders held in August 2006. Blocks put up for tender at the time are in Madang, 
South Mandar, Sageri, and South Sageri, all off the South Sulawesi coast; 
Enrekang, onshore in South Sulawesi; and Karama, Malunda, and Mandar, off West 
Sulawesi.<br>
