<p align="justify" dir="ltr"><font face="Verdana" size="1">The growth of Oman based hardware industry as a public-private partnership will 
increase PC penetration levels in the initial phase and create a regional market 
in subsequent phases. Once sufficient access points and telecommunication 
connectivity are made available, community-run satellite learning centres will 
boost digital literacy.<br>
<br>
The theory that IT systems replace existing employees no longer holds true. In 
reality, IT enhances the productivity of the employee by empowering him with 
potential knowledge and skills to exceed customer expectations. The evolving 
digital society, championed by IT literate youth and technology empowered 
employees as eCitizens, will steer eOman to greater heights.<br>
