<p align="justify" dir="ltr"><font face="Verdana" size="3" color="#FF0000">
<br>
</font><b><font face="Verdana" size="4">Digital Learning</font></b><font face="Verdana" size="1"><br>
<b>Sangeetha Sridhar emphasises on the Sultanate’s focus on education and 
training to become a Digital Society<br>
</b><br>
<i>“We are in the new era of Globalisation, shrinking the world from small to tiny 
and flattening the playing field. The dynamic force now is the new found power 
for individuals to collaborate and compete globally”.
Pulitzer prize winner Thomas Friedman in his book, ‘The World is Flat’ </i> <br>
<br>
Technology is the key enabler for transformation of society into an economically 
lucrative knowledge-based society. The creation of such a society lies in the 
digital literacy of the public, which enables them to adapt to the digital 
lifestyle and adopt electronic services in large numbers and volumes. <br>
<br>
In this progress, normally the less-abled sections of the society are left 
behind due to digital illiteracy, creating the digital divide and widening it 
farther and deeper. To bridge such a gap, e-government and IT strategies involve 
capacity building as a success criteria and hence a national goal. The follow-up 
report of the 3rd Symposium of Employment of National Labour Force in 2005 
indicates continuous expansion of the ICT sector. The need for both IT 
specialised skills and IT usage skills has increased manifold overtime. <br>
<br>
The current requirement for a qualified labour force in IT is being met by 
recruiting foreign labour, particularly in the private sector. The ratio of 
Omanisation in the IT sector, which stood at 7 per cent by the end of 2002 is 
targeted to reach 25 per cent at the end of 2008. <br>
<br>
<b>Towards e-learning</b><br>
To attain this objective, education and training are core areas of focus to 
build a sustainable national capacity. According to the 2003 Census, about 54.86 
per cent of the population requires tertiary or higher education. An additional 
23.89 per cent requires adult learning as higher education or on-the-job 
training. According to Annual Budget 2007, the current allocation for the 
education sector is RO609 million, up RO84 million or 16 per cent from the 
approved budget for 2006.</font></p>
<p align="center" dir="ltr">
<img border="0" src="spotlight4.jpg" width="419" height="162"></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">This is in addition to the development expenditure allocated for this sector, 
which is estimated to be about RO242 million. With 54.93 per cent of the Omani 
population below 20 years of age, focus of primary education on deeper level of 
literacy in analytical subjects like mathematics and science is vital for the IT 
sector. The pace of learning can be accelerated with a common pool of 
multimedia-based Arabic learning content, with free access for the education 
community. With 70.2 per cent of Omani schools having ICT facilities, leveraging 
learning content from local academic community requires a focused plan, 
spearheaded by a government body. <br>
<br>
With ICT as a catalyst, higher education can change radically, with improved 
rate of enrolment, more industry-oriented curriculum, highly engaging delivery 
of education, enhanced learning experience, broader assessment strategies and 
varied teaching styles, resulting in self-sustained and readily employable 
graduates.<br>
<br>
<b>Increasing access</b><br>
With His Majesty’s generous grant of RO17 million for Muscat, Nizwa, Dhofar and 
Sohar Universities, they are certain to expand their infrastructure facilities, 
labs and other requirements. The time is ripe to invest in IT infrastructure to 
improve the student vs. computers ratio and per-capita bandwidth. Offerings in 
Omani higher education are predominantly Baccalaureate degrees related to ICT. 
This must expand horizontally in the range of offerings and vertically with 
masters and research programmes in quantity and quality.</font></p>
