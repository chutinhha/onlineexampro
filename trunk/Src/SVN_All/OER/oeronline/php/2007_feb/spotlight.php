Growing @ the Speed of 
Thought</font></b><font face="Verdana" size="1"><br>
<b>In the last few decades, the information technology revolution has led to 
transformational economic and social shifts around the world. Today, the momentum of 
change is faster than ever before. Everyday, new technologies impact, in one way 
or another, an increasing number of industry sectors. The irrelevance of 
distance and time as a result of IT has opened up unprecedented opportunities in 
markets across the globe</b><br>
<br>
</font><b><font style="font-size: 14pt" face="Verdana">
<img border="0" src="images/spotlight.jpg" width="300" height="215" align="left" hspace="8"></font></b><font face="Verdana" size="1">The potential of IT rests on an unrestrained ability to process, store, 
retrieve, duplicate and transmit information, unconstrained by time, distance 
and volume. With integrated information systems, products and services are now 
increasingly becoming available to the smallest of enterprise and in the 
remotest of regions. While it took 75 years from its invention for the telephone 
to reach 50 million users, it has taken the World Wide Web (WWW) only four years 
to achieve the same. Some have termed IT as the second industrial revolution. 
Interactions, such as through the e-mail, have jumped 32 times from 20 million 
users worldwide in 1994 to 651 million in 2005. <br>
<br>
Latest estimates from the international market research firm IDC predict that 
worldwide end users will spend US$1.2 trillion on information technology in 2006 
and IT spending will increase at a compound annual growth rate of 6 per cent to 
reach US$1.5trillion in 2010. In the Gulf, the UAE and Saudi Arabia alone 
account for 77 per cent of the region’s current annual IT spend of US$4.94 
billion. Industry estimates show that the Middle East and North Africa (MENA) IT 
market is set to grow from US$6.9 billion in 2003 to US$13.4 billion by 2008.
<br>
<br>
Global IT spending growth trends indicate that the region is primed to spend 
more, with the governments across the region keen to bring in the latest 
technology. Various government and private sector organisations have been 
gearing up to adopt fresh and innovative technologies.</font></p>
