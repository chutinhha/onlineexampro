</font><b><font face="Verdana" size="5" color="#FF0000">The all-terrain predator </font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="1">
<br>
<b><img border="0" src="car.jpg" align="right" width="257" height="142">Land 
Rover’s LR3 is not for the weak-hearted but for those who love to rough it, 
discovers Anne Kurian.</b><br>
<br>
People tend to associate the 4-wheeler to men. Rugged, sturdy and powerful, a 
4WD is always thought to be a man's vehicle. I'm no feminist but this is 
something I totally disagree with. Especially after experiencing the LR3 from 
Land Rover. Yes, experiencing. For driving the LR3 is different from 
experiencing it. This giant of a 4WD is the perfect mate for just about anybody 
who loves to rough it out in Oman. It definitely is not for the tame but this 
beauty is just what will get your adrenaline rushing. Behind the wheels of the 
4835mm long and 2190mm wide LR3, I felt like a queen who's in total control of 
her vehicle and the journey ahead.<br>
<br>
The LR3, launched recently, comes as the successor to the popular Land Rover 
Discovery that has now been phased out. After spending a day with the new 4WD 
from Land Rover, we had no doubt that the LR3 would prove to be a worthy 
successor. It is comfortable, versatile, agile and a dream on and off road. And 
the team at MHD proved this to us when they decided to take us out on a test 
drive like none other we have experienced to a track discovered by them in Hamam, 
off Ghala. Before getting into details of the test drive, a lowdown of the 
interior and exterior features of the LR3.<br>
<br>
<b>Inside Out</b><br>
The LR3 is a picture of clean clear-cut styling with no hang-ups whatsoever 
you’d associate with a 4WD. In fact, it’s more of a minimalist when it comes to 
the exterior features. Strong yet simple lines define the LR3, which is big in 
size yet has a sporty look. Functionality is a keyword for the makers. One of 
the simplest examples is the rear taildoor — an easy-to-open clamshell style for 
the upper half, and a regular flip down for the lower half. The flip down 
taildoor can double up as seats when you want to park the LR3 overlooking a 
mountain cliff or on the beach.<br>
<br>
One interesting feature was the absence of the spare wheel on the door of the 
rear taildoor, as is the norm. What the engineers of this 4WD have done is to 
put it away undernearth the chasis, which has helped them to separate the 
taildoor into two. Now this is an innovation, but getting under the vehicle and 
removing the spare in case of an emergency can be quite tedious when compared to 
the convenience of taking out a back door mounted or floor-mounted spare. <br>
<br>
Moving onto the interiors of the LR3, it would be an understatement to say that 
this is one spacious vehicle. The LR3 boasts of comfortable seating for seven 
people, with three rows. The LR3 provides for a ‘theatre style’ seating with 
each row of passengers getting a good view of the road ahead. The third row can 
be flipped down to accommodate extra luggage or other paraphernalia you’d take 
on a trip. The leg space is impressive. And so is the firm and fairly large 
seating. <br>
<br>
The LR3 comes with a six CD changer and six speakers that we tested out on the 
roads. And were we impressed or what! <br>
<br>
<b>On and off road </b><br>
A 4WD serves its purpose when it shows what it can do on and off the track. And 
that was what I was waiting for. I was a bit skeptical about having the sales 
engineer from MHD join me on the test drive for I wanted to try out the LR3 to 
my convenience. But it did not take him much to convince me to go to their 
particular test track in Hamam. It was where they took all their prospective 
clients. So it had to be good and right for the LR3. Once we got there I saw why 
he was insistent on bringing me to the test track chosen for the LR3. Our usual 
test drive areas like Qantab, Athaiba and Ghubra paled in comparison to this 
one. Rocky, uneven and precipitous, the stretch had to be discovered on the LR3 
to be believed. <br>
<br>
The track in Hamam, which comes much beyond Ghala was discovered after much 
exploration by the team at MHD and Land Rover for the perfect testing ground for 
the 4WD. They have definitely located one of the best test tracks for a 4WD. One 
path that came to mind when we were in Hamam was the Khour Rouri stretch in 
Salalah, which is almost as rocky and uneven, though the LR3 test track takes 
the cake.<br>
<br>
Getting back to the drive, we proceeded to test out the new features of the LR3. 
The Land Rover’s ‘Terrain Response’ is the bedrock of the LR3’s off-road 
capability. The Land Rover engineers are said to have examined over 50 different 
off road terrains and determined the engine, transmission, suspension and 
traction to optimise the vehicle’s performance in each terrain. They finally 
narrowed down five terrains – road, snow, mud, sand and rocks. <br>
<br>
We were mightily impressed with the LR3’s performance on the offbeat difficult 
uneven path strewn with rocks of various sizes. Climbing down a couple of steep 
paths was easy in the LR3 that seemed so in control of the path. One reason, 
though, for this smooth descent was the Land Rover’s patented Hill Descent 
Control (HDC) brake system, which controls the speed when moving down a steep 
path. <br>
<br>
There were moments on the rocky - at times, daunting - uneven terrain that the 
LR3 reminded me of a predator. Going over rocks and even a narrow pathway that 
had the LR3 tilted in a precarious manner while driving seemed like child’s play 
in the 4WD. <br>
<br>
The road grip of the LR3 is commendable especially when you do not see a sharp 
turn ahead, like it was in our case. The 4WD is considered to have performed 
well on the sand dunes in Bowsher; we unfortunately could not test that out but 
we certainly do not undermine the potential of the LR3.<br>
<br>
Back on the roads of Muscat, the LR3 was as good as any saloon car. It moved 
with agility and precision. And the smug feeling of towering over the others on 
the road was something my friend and I found hard to suppress. <br>
<br>
<b>Safety</b><br>
It’s a Land Rover after all; safety is high on the cards. The LR3’s two 
brake-based systems — Hill Descent Control and Dynamic Stability Control (that 
allows you to drive uninhibitedly on rough tracks yet be in control without 
skidding) — leave you with little to fear, on or off road. <br>
<br>
The Electronic Park Brake comes in handy when you have to brake suddenly. This 
does away with the screeching of tires when you stamp down the brake and stops 
the vehicle within seconds. <br>
<br>
To sum up, Land Rover knows how to satisfy a customer and the LR3 is yet another 
shining example of their technological advancement and expertise. And if you 
think you can rough it out, then the LR3 is surely your companion, on and off 
road! </font></p>

