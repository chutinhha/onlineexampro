<p align="justify" dir="ltr"><font face="Verdana" size="1">They don’t believe in taking full-page advertisements every day in newspapers to 
say, ‘Look, how great we are’. They work in a manner which is more discreet, 
more business oriented. Their philosophy is that let our project speak for 
itself. At The Wave, we share the same credo. We also believe in showing people 
what we do rather than blowing our own trumpet. I don’t think they are keeping 
low profile. Oman does not make a lot of hype. <br>
<br>
<b>Dubai is one of the leaders in terms of water front investments. Are you 
trying to be slightly different from those projects? </b><br>
Dubai is pioneering in more than waterfronts. Oman has pioneered in waterfronts 
historically. It has established waterfronts in commercial and residential 
respects. Omanis are not new to living next to sea with ships next to them. In 
terms of modern waterfronts, this is indeed one. Dubai is showing its way 
forward. I think we will never compare in terms of the size of the developers or 
investments. Look at the audacity or the sheer courage of what they’re doing. 
Hats off to them.<br>
<br>
We will be different. Its in Oman and it has to complement. That is the reason 
we have consciously been saying that there are certain components we don’t want 
to have in our projects. Because there are certain things that people can do 
outside. In Oman, they should go and see the shore. They should go diving. We 
give them an excuse, the ease, the product, the pleasure the experience. The 
luxury of being here. That’s our philosophy. The heritage angle is bit 
different. In terms of components, all the present developers should agree that 
we are different. We are doing golf courses, marinas, houses, hotels etc. Now, 
its about how you play around. There are so many magazines treating the same 
subject in different ways.<br>
<br>
<b>Speculation is rife that the Wave project will have several firsts? Is that 
so?</b><br>
Well, we will have some. But we are not in a superlative game - because if you 
are first, someone else will beat you sometime. We are not into creating 
something, say the biggest, the largest or the smallest or the tiniest. We are 
looking at creating something that will sustain the test of time and want it to 
be a successful community where people will live, go to work. And there will 
shops, hotels and markets and everything else. That is our criteria and our 
vision. <br>
<br>
That’s what we want to do. In the process, we have become the first Signature 
Golf in the market in the past seven months. We will bring in lot of hotels that 
will have first time presence in Oman. You can see from our model that we have a 
harbour and island that you cant see anywhere in Oman. The whole thing is very 
unique. <br>
<br>
<b>How did the concept of Signature Golf Course come about?</b><br>
To be honest, when started we did not have a signature designer because we felt 
may be we will be the first - may be the second - golf course in Oman. We had 
very few plans at that stage . In the Gulf Cooperation Council region, there 
over 20 golf courses. The prominent ones are mainly in Dubai. In Oman, we don’t 
have any complete golf courses.: One in Salalah and another in Muscat. But, no 
18 hole full-fledged green course. We want to be different. The Signature idea 
was to make sure that nobody beats us. Of course, they are welcome to match but 
not beat us!<br>
<br>
<b>What is your pricing strategy?</b><br>
We could not have come so far in a project without having very clear views on 
what our prices will be for many reasons. At this stage, I cannot announce the 
prices. Obviously, it is going to be at a premium compared to other developments 
in Oman. For many people this represents a better quality of life. New lifestyle 
will surely develop. In terms of the region, this will be very competitive. <br>
<br>
<b>So, you are targeting at the ultra rich…</b><br>
No, we are not. In future, there will be projects targeting them. Right now, we 
are focused on the upper middle class Omanis and residents of Muscat who 
understand the place, like the place, who want to reside and continue to grow 
here as their final home. I see it as a final home and safe investment for the 
future. Once we come out of those two segments then we will look at the 
expatriate in the region. In terms of countries of origin, we will be looking at 
Omanis, the Indian sub-continent and western Europeans. <br>
<br>
<b>Beyond the model put up at the sales site, when do you reckon actual sketches 
will be made available?</b><br>
Right now, we dish out CDs that will give our architectural style. The actual 
house design will be released very soon. </font></p>
