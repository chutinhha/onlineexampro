<img border="0" src="main2c.jpg" width="400" height="174"><font face="Verdana" size="1"><br>
<br>
The pace of development at Sohar is at its peak. The port city is buzzing with 
machinery, construction and industrial activity. Let us take a glimpse into the 
major happenings:<br>
<b><br>
Pact on container terminal:</b> Hutchison Port Holdings (HPH), the world’s 
largest independent port operator and leading port investor, has worked out an 
agreement with the government to set up an international container terminal at 
the Sohar Industrial Port. The new company, Oman International Container 
Terminal (OICT) will be a joint venture between HPH, the Oman Government, 
Steinweg of the Netherlands and a number of well-established Omani investors. 
While HPH will hold a 65 per cent stake in OICT, the Oman Government 22 per cent 
stake, Steinweg 7.5 per cent; Royal Office Pension Funds 2.5 per cent and local 
investors the rest. HPH has signed another deal with Sohar Industrial Port 
Company (SIPC) to invest and operate the container terminal at the port of Sohar, 
managed by SIPC. <br>
<br>
Maritime college: The foundation stone of Oman International Marine College was 
laid in November in the presence of His Highness Sayyid Shihab bin Tariq Al 
Said, His Majesty the Sultan’s advisor, and HE Sheikh Mohammed bin Abdullah Al 
Harthy, Transport and Communications Minister. The maritime college would be the 
first and the only college in the Sultanate for education and training in 
navigation, maritime engineering, port, shipping and transport management, and 
operational technology.<br>
<b><br>
Plastic Institute:</b> The government is also planning to establish a 
specialised institute in Sohar for making available a pool of skilled 
professionals to work in the downstream plastic industry and the petrochemical 
sector. <br>
<br>
<b>Sohar Development Office: </b>HE Sayyid Saud bin Ibrahim Al Busaidi, the 
Minister of Interior, recently presided over the opening of certain development 
projects at the Sohar Developmental Office as part of the 35th National Day 
celebrations.<br>
<br>
Sohar power and water project: The construction of Oman’s biggest greenfield 
project viz., the Sohar Independent Water and Power Project is in advanced stage 
of construction at Sohar Industrial Port. The project is scheduled for a partial 
start-up in April next. Nevertheless, the plant’s full capacity of power and 
water production will be available for dispatch by April 2007.<br>
<b><br>
First 100 per cent private downstream project:</b> Project agreements linked to 
the development of a formaldehyde plant at the Sohar Industrial Port are 
expected to be finalised soon. Oman Formaldehyde Chemical Company LLC, a 50-50 
joint venture of Omar Zawawi Establishment (Omzest) and the Saudi-based Manso 
Group, at a cost of $21.5 million, are developing the facility. <br>
<br>
The facility will initially produce urea formaldehyde to meet the demand from 
the urea projects of the Oman-India Fertiliser Company (Omifco) at Sur and Sohar 
International Urea and Chemical Industries (SIUCI) at Sohar. This project 
represents the first 100 per cent private sector downstream industry at Sohar.<br>
<br>
Sohar Aluminium’s loan deal: Sohar Aluminium Company (SAC) is one of the biggest 
industrial establishments in Sohar. The financial closure of SAC for funding the 
multi-billion dollar project is round the corner. Three lead arrangers 
comprising ABN Amro, Sumito and Citibank will arrange the term loan of $1,460 
million. SAC has signed up with Alcan for exporting products to third countries 
and expected to offer permanent job opportunities for a thousand at least.</font></p>
