<br>
<b>My management style and structure</b><br>
The management structure followed in our company is simple and we are using a 
functional style. There are three senior level managers — Finance, Operations 
and Sales and Marketing — reporting to me. I work very closely with these three 
people but they are independent in their own right. Weekly meetings are held to 
review and analyse business strategies. It is the teamwork that has helped us 
reach where we are after we resumed work in 2004. <br>
<br>
I do not believe in following one particular management style as the General 
Manager. To me, it depends on the people I am dealing with and the particular 
situation at hand. As a whole I am a very accessible person and tend to know the 
names of all the employees in the company. To be a good manager, you should be a 
good leader. That’s what is important. You should be able to motivate people and 
makethem work together as a team to achieve the company’s objectives. <br>
<br>
<b>Industry integration</b><br>
Sohar Poultry is shareholder company, owned jointly by a group of Saudi and 
Omani investors. Our company produces high quality poultry products. What is 
unique about us is that we have what is called in our industry ‘ a vertical 
integration’. This means that we are the only company in this business to have 
our own feed mill, breeders, hatcheries, farms, processing plant and 
distribution facilities. At the moment, we are the only company to have all 
these facilities. Distribution is an important aspect in this business and since 
we are close to our market (our plant is in Barka) it is easy for us to deliver 
chicken products fresh daily; we reach the market in Muscat in less than an 
hour. <br>
<br>
<b>Industry jottings</b><br>
Though there is competition, our strategy was not to get into a neck-to-neck 
race. We used the ‘flanking strategy’ to get a footing in the business. What we 
did and are doing is to sell high quality products at an affordable price. This 
strategy has worked wonders for us. We have been in the market for only three 
months but gained a substantial market share. We have also been able to create 
brand awareness and brand loyalty for our brand, Rayaf Farms very quickly. We 
deal with food and, therefore, quality is very important. If they are not happy 
with a product, then they will not come back for more. In our case, customers 
have continued to buy our products, which speaks for itself. We have been able 
to give good quality products at competitive rates because we have industry 
integration. Oman’s poultry industry is in its infancy. The first producer of 
poultry products came into business in 1998. There is a lot of scope for growth 
here. We would like to help people realise that chicken is best fresh and not 
frozen. People in this part of the world and all of Gulf are used to eating 
frozen chicken as it is imported due to less local production. We would like to 
help people switch over to fresh chicken, which is one reason we have priced our 
products at a very competitive price.<br>
<br>
<b>Improvement factor</b><br>
Currently, poultry companies are offering consumers very basic chicken products. 
We would like to offer a larger range of chicken and poultry products. This 
would include ready-to-cook, pre-cooked and even marinated chicken. We would 
also like to see more stores having chilling facilities. The bigger stores have 
chillers but there are many smaller stores that do not have chillers and we are 
not able to sell our products there. With the industry improving in Oman, we 
hope this will be taken care of. n<br>
