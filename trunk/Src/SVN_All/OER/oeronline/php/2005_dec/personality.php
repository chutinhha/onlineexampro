<p align="justify"><font face="Verdana" size="1">Companies, like living 
organisms, mutate over time. Surviving a century is a big deal. Ensuring that 
the business remains with the original promoters is a challenge. The 
Oman-headquartered Naranjee Hirjee group does all of the above. OER catches up 
with Sudhir Keshavji, the third generation promoter and current Managing 
Director of Naranjee Hirjee, to walk down memory lane. <br>
<br>
</font><font face="Verdana" size="3"><b>E</b></font><font face="Verdana" size="1">mpirical 
studies reveal that only three per cent of family-run businesses make it to the 
third generation. Naranjee Hirjee’s fourth generation does not subscribe to this 
formula, they are in the saddle, involved in the day-to-day running of the 
family empire, and making history in Oman.<br>
<br>
Way back in 1905, Naranjee Hirjee took a dhow from his hometown Mandvi in Kutch, 
India, to Oman, then known as Muscat. He remarked once that “he simply went 
west”, stopping in Oman because of its seafaring connections with India, recalls 
Sudhir Keshavji. All that the patriarch had on his back were his clothes, and 
empty pockets.<br>
<br>
But he had a vision, no doubt. “What exactly he saw in Oman, we cannot be sure, 
as there was not much here at that time,” Sudhir offers as he walks down memory 
lane trying to recollect and piece together what he had heard from his parents 
and others. But before long, his grandfather had set up a small business of 
importing foods, such as rice, sugar and coffee, into Oman, and as a family, 
they never looked back.<br>
<br>
Slow &amp; Steady: The small business slowly grew and by the time of his untimely 
death at the age of 43, Naranjee Hirjee’s four sons, Jayram, Hansraj, Chaturbuj 
and Keshavji, kept working on their father’s vision, and expanded the business 
in Oman as well as setting up a sister company in India. In a bold move The 
Hirjee clan were the first to import a privately owned car – a Ford V – into the 
country back in 1934, an incident that sparked a life-long fascination with cars 
that still runs through the generations of the family.<br>
<br>
Sudhir, son of Keshavji Naranjee, was born in Oman. Nationality was bestowed on 
the family after they had lived in the country for more than 65 years. He has 
always seen Oman as his home, only leaving to be educated in India, due to the 
then lack of schooling facilities in Oman. His father expanded the business to 
include sole agencies for kitchen appliances such as Braun, Sharp and Kenwood, 
working from an office-cum-residence in old Muscat near the Palace.<br>
<br>
Keshavji’s sons Sudhir, Mahendra, Mahesh and Narendra worked in the ever growing 
business, which by 1960 was Oman’s largest importer of rice, and one of the 
leading businesses in the country. In the 1970’s, the family business 
diversified into construction with the help of a partner, Assarain International 
Construction Company. Thanks to the general boom of construction, progress and 
development Oman was experiencing at that time, the company spread its wings 
into other areas of business as well.<br>
<br>
The 16th of November this year, was a red letter day for them as it was time to 
celebrate the centenary year of the company. It is growing from strength to 
strength. Having started with just one visionary founder, the company had some 
10 employees in 1960, and now the main company employs 125 people, but boasts 
more than 800 employees taking all the diverse subsidiaries into account. And 
diverse they are. <br>
<br>
Diverse Interests: From being a rice and spice importer, Naranjee Hirjee’s list 
of interests now includes a lot: agencies for electrical and kitchen supplies 
(Gillette, Braun, Electrolux and Rubbermaid etc); import agencies for chemicals, 
including aromatic solvents, drilling mud chemicals, glass fiber products and 
many other products. <br>
<br>
The future lies in further diversification, says Sudhir candidly. That perhaps 
explains the group’s foray into restaurants such as Pizza Express, Kamat 
Vegetarian Restaurants and the Golden Oryx Chinese Restaurant. It is a founder 
member and promoter of the Salalah Hilton Hotel, owns businesses selling Bakhor, 
Frankincense and Arabic perfumes.<br>
<br>
It has its own public relations and event management company, Gazelle 
International, and has reached out into other areas such as real estate 
development, software provisions and higher education counseling. The Indian 
sister company, run by brother Mahesh, has recently secured the agency for the 
import of prestigious car makers Mercedes, Honda and Audi; and sister companies 
are successful in Somalia, dealing in scrap, and Dubai, dealing with diamonds 
making Naranjee Hirjee a truly international company. <br>
<br>
<img border="0" src="perso1.jpg" align="left" hspace="5" vspace="2" width="100" height="112">Success 
secret: To this day, the four brothers run the company’ in both Oman and India 
jointly. “We are an extremely close family. Always have been. All business 
decisions are taken together and all proposals discussed together. We even have 
homes next to each other. We like to be close,” confesses Sudhir. <br>
<br>
So what is the secret, apart from a tight family union and a keen eye for 
business opportunities? It is a mix of honesty, trust and good service – and, 
again, family ties. “Our company has always prided itself on providing honest 
service; we have gained the trust of the Omani people, businesses and customers 
alike. We will do nothing to risk losing that. We have a strong good reputation 
and that is important to us. Also, we value our employees. When we hire someone 
new, we always say that they do not join a company, but our family. We look 
after each other and hope that all our employees will be with us for many years, 
so that trust and values are instilled and adhered to. The family might be the 
heads of the company, but our employees are the hands, without which nothing can 
happen,“ Keshavji says in an emotion-choked voice.<br>
<br>
The fourth generation came into the family run business with the induction of 
the Sampat siblings: Sameer, Amit, Nikhil and Devang. <br>
<br>
Steering the group to greater heights should be a small challenge given their 
genes. Beating the statistics and operating for another 100 years won’t be 
child’s play but if the children play it right, who knows what new horizons they 
will conquer? n</font></p>

