            <p align="justify"><b><font face="Verdana" size="3"><br>
            I</font></b><font face="Verdana" size="1">n a matter of a 
            few days, the year 2005 will sign off and what will remain of it is 
            a few frozen frames in time which will always be remembered for 
            their ramifications. Frames which depict the important events, both 
            positive and negative, at the national and international levels and 
            which will have a bearing on developments in the future. Oman 
            Economic Review takes stock of some of these developments to present 
            you a collage of the year that has been. Or rather soon we would 
            say, the year that was. <br>
            <br>
            <b>Oil on the boil </b><br>
            In 2005, nothing impacted the global economic scenario more than the 
            spiraling prices of crude. Economies of all hue and nature - 
            developed or developing - were taken aback by the abnormal rise in 
            the price of crude, which touched its historic high figure of over 
            US$70 per barrel. <br>
            <br>
            The oil producing countries, however, made most of the killing as 
            their revenues multiplied manifold. The Gulf Cooperation Council (GCC) 
            countries, including Oman, emerged as the major beneficiaries from 
            the spike in the oil revenues. The increased income encouraged the 
            governments in the GCC countries to invest more in the 
            infrastructure development exercises to insulate their economies 
            from expected drop in oil revenues in the long term. <br>
            <br>
            But the heavy oil consuming countries, including China and India, 
            which are recording a robust growth above 7 per cent faced the 
            burden of a sharp increase in their import bills. Economic 
            powerhouses, including the United States and many European 
            countries, were not spared. As an immediate impact of the ‘oil on 
            the boil’ syndrome, these countries saw their inflation rates 
            soaring. <br>
            <br>
            <b>Turnaround in tourism </b><br>
            Four years after 9/11, tourism in the Middle-East countries 
            displayed the signs of a strong turnaround with most of the 
            countries registering record inflow of tourists. Unsurprisingly, all 
            countries in the region were seen initiating new measures to 
            capitalize on the tourism boom to give a boost to their respective 
            economy. This also practically meant making fresh investments 
            running into staggering sums in the billions of dollars. For 
            instance, in the Sultanate of Oman, Al Madina Al Zarqa (Blue City), 
            a US$15 billion ultra modern tourism and residential city project, 
            was announced. Besides, the work on the Wave project and Muscat Golf 
            Course project continued in full swing. The Shangri-La’s Barr Al 
            Jissah resort complex is also ready for launch.<br>
            <br>
            Prominent airlines including Emirates and Etihad are ready with 
            plans to invest billions in fleet expansion and sprucing up their 
            network to handle the increased air traffic. As per estimates, the 
            Middle East region is expected to pump in US$124 billion into new 
            aircrafts to meet the extreme growth in air traffic. <br>
            <br>
            <b>Aligning with global economy</b><br>
            The GCC countries’ quest to become an integral part of the global 
            economy got a major boost this year as Saudi Arabia got the nod for 
            its entry in the World Trade Organization (WTO) and Oman signed a 
            Free Trade Agreement (FTA) with the US in record time. The closer 
            economic ties with other major economies are opening new avenues of 
            foreign direct investment (FDI) for the GCC countries. <br>
            <br>
            <b>New initiatives in oman </b><br>
            The Sultanate of Oman made further concrete moves to liberalize and 
            privatize its economy. The government owned telecommunications 
            giant, Omantel, has gone public with its successful initial public 
            offering (IPO) and it received an overwhelming response when it made 
            its debut at the Muscat Securities Market (MSM). The entry of a 
            private mobile communications service provider Nawras in the market 
            was yet another significant development of the year. With Nawras 
            becoming operational this year and Oman Mobile pulling up its socks, 
            mobile telephony in the country is moving. Customers have felt the 
            impact in lower tariffs. <br>
            <br>
            The government’s Omanization drive is bearing fruits for the 
            Sultanate. The Ministry of Labour figures showed that the number of 
            expatriate workers employed by private companies and institutions 
            had fallen by 24.4 per cent since January 2003. On the flip side, 
            the inflation rate has gone up in the Sultanate with consumers 
            raising their concerns on the hikes in commodity prices. The 
            government has taken active steps to control the rising inflation.
            <br>
            <br>
            <b>Sad stories </b><br>
            The Year 2005 will also be known for the death of hundreds of 
            thousands of people in natural disasters, terrorist attacks and 
            accidents. Hurricane Katrina swept the Gulf of Mexico, resulting in 
            losses of over US$100 billion besides raising doubts about the 
            readiness and capability of the most powerful nation on the planet 
            to deal with disasters of such magnitude. <br>
            <br>
            Thousands of people died in the earthquake, which struck Pakistan 
            and India. More than a thousand lost their lives in a stampede in 
            Iraq. The terrorist strikes in England, Egypt and Jordan etc. left 
            hundreds of innocent people dead. France witnessed a bout of riots 
            across the country, which forced the French government to impose a 
            state of emergency in the country. The number of displaced and 
            homeless as a result of natural disasters is in the millions.
