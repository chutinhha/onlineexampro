            <b>‘Use’ your employees – without making them feel used</b><br>
            New managers sometimes let the rush of authority go to their heads. 
            They suddenly feel awash with entitlement. They start to view 
            themselves as special, different, superior. They may exempt 
            themselves from standard rules of decorum, such as saying ‘please’ 
            or ‘thank you’.<br>
