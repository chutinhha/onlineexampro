            For example, I met a marketing manager, Wes, who was six weeks into 
            his new position. His bosses couldn’t understand how Wes had 
            faltered so fast. He had alienated his employees by imposing all 
            kinds of crazy edicts, ranging from an absurdly rigid dress code to 
            having them log their day’s work in 15-minute increments on 
            elaborate reporting form that he designed and distributed. The staff 
            was verging on mutiny. <br>
            <br>
            Wes wasn’t a villain. He had just lost his bearings. As he put it 
            sheepishly to me, “I guess I got carried away by this whole thing. 
            Being in charge brings out another side of me even I don’t always 
            recognize. Sometimes I think my people are tools to be used to get a 
            job done, and I forget they ‘re actually human beings.”<br>
            <br>
            I advised Wes to settle down, draft a management credo, and make 
            corrections in his leadership style. Even though his employees saw 
            him as a micromanaging tyrant, the truth was he had lost his 
            confidence and overcompensated by tightening his grip over his 
            staff. <br>
