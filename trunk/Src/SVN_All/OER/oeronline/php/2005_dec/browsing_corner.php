            still find a way to harness your employees’ talents to maximum 
            effect. Put crassly, that means using your employees to make you 
            look good. Put not so crassly, it means working together to make the 
            whole group look good because all employees and their manager are 
            succeeding. The effective manager does this wisely and with full 
            buy-in of employees, rather than exploiting them shamelessly and 
            then hogging the credit for their hard work.<br>
