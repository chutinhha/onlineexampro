<p align="justify" dir="ltr"><font face="Verdana" size="1">
<b>As you aim to move up in the value ladder chain, how do you plan to meet the 
challenge of changing people’s perception of Hyundai from a low-end segment 
brand to a middle and upper segment brand?</b><br>
It takes time to change people’s perception. We can’t change the brand image in 
a very short time in the Middle East. Still, we have managed to change our brand 
perception in markets including the United States and Europe already. <br>
<br>
Over the last five years, Hyundai has improved in IQS (Initial Quality Survey) 
ranking at double the industry average and was second in the JD Power and 
Associates 2004 IQS, trailing the industry leader by a single point. Our 
fixation on quality has critics’ heads turning and we are only just beginning 
our quest to become the leading global automaker. And this is reflected in our 
communication to the trade and consumers.<br>
<br>
JD Power &amp; Associates ranked Hyundai Sonata first in its IQS of entry-level 
mid-size cars. The Sonata outpaced even the Toyota and its ranking caused quite 
a stir in automotive circles. In the mid-size segment, Sonata has been rated at 
par with Toyota’s range of automobiles. On certain parameters we are, in fact, 
rated higher than any other auto brand. This kind of evaluation is recognized by 
consumers who look for high technology, excellent performance and reasonable 
pricing.<br>
