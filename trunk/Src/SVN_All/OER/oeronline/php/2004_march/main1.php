<p align="justify"><font face="Verdana" size="1"><b>&nbsp;The Wave Theory
</b><br>
The Seeb seafront project, Muscat's largest and most eagerly awaited resort 
development,
will be called &quot;The Wave&quot;. The government revealed the brand name and
also the project logo at the end of last month. Stretching along 7.3 km of 
unspoiled
beachfront just west of Muscat, the RO310 million resort project is ready for 
development
and awaiting a wave of investors to embrace and foster its growth.<br>
<br>
&quot;In light of Oman's rich seafaring history and close association with the
ocean, as well as the projected shape for the new development, The Wave is the
perfect name to give the resort its own distinctive identity,&quot; says His 
Excellency
Abdullah bin Abbas bin Ahmed, head of Muscat Municipality and chairman of
the Oman Tourism Development Company (OTDC).
<br>
<br>
Abdullah Abbas also revealed more details about key features and attractions
of the planned resort. Three premium hotels, each with its own distinctive
theme, a golf course and marina with yacht club, and luxurious villas and
upscale apartments are planned for The Wave.
<br>
<br>
Together with the project's financial and marketing consultants, CII Group -
Middle East (M.E), the strategy consultants OTDC appear confident that
investors will participate in this exciting development. For the first stage, 
RO32
million will be invested to reclaim land, develop the infrastructure and build a
golf course and a marina with yacht club for the two million square metre plot
along pristine Al Athaiba/Al Hail seafront coastline. Once this is in place, 
developers
will invest an additional RO186 million to put in all the elements needed
to build an exclusive resort with a final value of RO310 million.
<br>
<br>
&quot;We have a clear vision for The Wave,&quot; Wael Al-Lawati, acting CEO of
OTDC, confirms. &quot;The Wave is designed to offer an upscale living and visiting
experience that is integrated into the Arabic culture, heritage and geography of
Oman. It shall become one of the key leisure attractions of the capital region&quot;.
<br>
<br>
This isn't Oman's first foray into resort development. The Zubair Group with
the support of local government is currently constructing the Barr Al Jissah 
Resort
which will consist of three luxury hotels to be managed by Shangri La.
<br>
<br>
<b>Surf's up!
</b><br>
Time will tell if investors
will be prepared to ride
The Wave. Meanwhile,
those involved in the project
are adamant that it
makes for an extremely
attractive prospect that will
beckon tourists and
investors to Oman creating
ample employment opportunities
in the process.</font></p>

<p align="justify"><b><font face="Verdana" size="2" color="#FF0000">Excerpts from the interview
with Abdullah Abbas:
</font></b><font face="Verdana" size="1"><br>
<b>Isn't Oman slow to develop its
tourism industry compared to its
neighbours in the region?</b><br>
&quot;I personally like to focus on what we
plan to do and the exciting times ahead
of us instead of looking backwards.
The Directorate of Tourism has wellstructured
plans in place to ensure that
Oman takes its rightful place on the
global tourism map. Our focus is on a
long-term plan to ensure that our
approach and our attitudes are keeping
in pace with global changes.&quot;<br>
<br>
<b>Will there be a wider change in law to
allow expats from outside of the GCC
to own property in Muscat and The
Wave resort project?
</b><br>
&quot;As you know a recent law has been
passed for the GCC nationals and so we
hope a similar law will be passed for
expatriates from elsewhere in the near
future in line with regional trends.&quot;
<br>
<br>
<b>One criticism of the The Wave is the
money being spent to reclaim land
when there appears to be plenty of
undeveloped seafront. Couldn't the
money have been put to better use?</b><br>
&quot;Our current estimates on how much
will be spent on land reclamation are
between RO15 million to RO20 million.
The reason behind land reclamation is
to add to a unique shape to the land
which is otherwise very pristine but
lacking any shape. Besides, our intention
is to focus within the capital area
to ensure that the developments
planned here blend with the existing
development and attract investors.&quot;<br>
<br>
<b>What sort of clientele do you expect
spending time at The Wave with it being
so close to Seeb International Airport?
</b><br>
&quot;The project vision is to cater to the
tastes of the international &amp; GCC visitors,
home owners and local residents.
The components have been chosen with
this clientele in mind. The close proximity
to the airport is an added advantage
since it allows the visitors to be at a
world-class hotel within minutes which
offers all the facilities that they would
expect to find in an integrated resort.
Having said that, the resort is also is also
close to the other attractions in Muscat.&quot;
</font></p>

<p align="center">
<img border="0" src="46.jpg" width="400" height="145"></p>

<p align="justify"><font face="Verdana" size="1"><b>Are there any environmental considerations
involved with the development?
What sort of feasibility study
has been conducted for the project?
</b><br>
&quot;As you are aware, environmental conservation
is a cornerstone of our development
approaches and this project is no
exception. We had prepared a preliminary
environmental impact assessment
report as part of the conceptual design
phase and this will be further detailed
and submitted as the project progresses.
<br>
<br>
The project started with a design
competition for possible development
options. Since then we have completed
an extensive financial feasibility study,
financial model and investor related
documentation. Currently CII-ME are
managing the investor promotion.&quot;
<br>
<br>
<b>Will the prices of the rooms at the
hotels in The Wave be in line with luxury
rooms elsewhere in the Muscat area?
</b><br>
&quot;The resort will have several hotels in
the various zones each with a different
market segmentation and targeted customer
audience. The hotels will include
a 5-star + with an exclusive beach, a
golf/conference hotel and then a hotel
in the marina which will be 3-4 star. So
as you can see each of these will have a
different pricing strategy.&quot;
<br>
<br>
<b>How will the 18-hole golf course be
watered? Will this be linked to the
Muscat Wastewater project? How is
the project doing?
</b><br>
&quot;Indeed, we are utilising the synergy
with the Muscat Wastewater network
that will be available and so the resort
will be linked to the network in terms of
both discharging the waste generated
and obtaining water for the golf course
in addition to other parts of the project.
<br>
I am pleased with the progress
achieved in the short period since the
commencement of operations of the
Oman Wastewater Services Company
who are pressing ahead with the
design and construction of a modem
sewerage network for Muscat.&quot;
<br>
<br>
<b>Which foreign investors have so far
approached Oman on this project?</b><br>
&quot;At this stage the focus is on the
investors and so we have been
approaching international and regional
investors to be a strategic partner with
the government in developing this project
and it is heartening to see the enthusiastic
reaction as they can see the
potential of this project. We have also
presented this opportunity for the local
investors. It is too early to reveal names
since we are in the process of discussing
it with several parties but I hope that we
will be able to announce our strategic
partners in the near future.&quot;
<br>
<br>
<b>How many other Muscat-related
tourism projects are currently there on
the drawing board?
</b><br>
&quot;I am certain you are fully aware of the
Barr Al Jissah Resort and the Muscat
Golf &amp; Country Club projects which are
being developed at the moment. I assure
you that we have more coming but let
us keep that under wraps while we further
develop the concepts and package
it in a manner that will allow investors
to immediately join these project.&quot;
<br>
<br>
<b>How much employment will this project
create? How many workers are
needed after its completion? Are there
enough hospitality industry training
institutes in Oman to cope with the
number of employees envisaged or will
trainees need to look aboard?
</b><br>
&quot;This project is likely to create around
2,000 jobs. The plan is that the various
developers will set in place a plan for
the development of sufficient number
of Omanis for the hotels and other project
components. With the upcoming
hospitality institute, and careful planning
this should not pose a challenge
for the developers to meet the stipulated
Omanisation targets. As is now evident
more young Omanis are heading
towards the hospitality industry and so
there is no shortage of interest.&quot;
<br>
<br>
<b>Will the biggest tourism project in
Oman's history finally put Muscat on
the global tourism map? Has the
Muscat Festival helped to achieve this?
</b><br>
&quot;All I have no doubt that this and the
other projects in Muscat are going to
place us on the global map in a formidable
manner. No visitor to Muscat
leaves without impressed with the
beauty, history and modem amenities.
With the launch of these tourism
focused products, we will be able to
widen the offering and attract more
tourists who want to explore the rich
Arabian history which we are uniquely
placed to offer.<br>
The Muscat Festival has helped
showcase Muscat for the regional visitor
and has highlighted our cultural
heritage. Very soon we will be able to
integrate these projects into the other
plans for promoting Muscat as a unique
and memorable tourist destination.&quot; </font></p>

