<p align="justify"><font face="Verdana" size="4" color="#FF0000">Becoming accountable</font></p>

<p align="justify"><b><font face="Verdana" size="1">
<img border="0" src="im2.jpg" align="left" hspace="5" width="200" height="250">Afshan Patel looks at the reasons why accountants cook the books, the impact of 
US state laws brought
in to reduce this risk, and relevant regulation in Oman to combat accounting 
scams
</font></b></p>

<p align="justify"><font face="Verdana" size="1">In recent times, there have been
surprisingly more scandals that
have come to light in the corporate
world than even in the public
sphere of politics. Yet more
often than not you'll be able to find
companies which win favour in the
market and shine above others. The
business media, ever watchful for star
performers as well as business failures,
gives these companies extensive
coverage. In subsequent years, a star
performed can soar even higher, it can
begin to plateau but remain vigilant,
or it can begin to dip. How well the
particular company performs will
depend on new competition, the
firm's management and many other
factors within its business environment.
One thing is certain, however.
Having already tasted the limelight,
the pressure to do better every year
(year-on-year) increases. Although it
is not impossible to grow continuously,
especially if new markets are being
tapped and the product range is being
expanded, there are times when successful
businesses fail to develop as
fast as their key shareholders or owners
would like. This fact becomes is
too difficult to digest for some, particularly
for those where the hunger for
business growth is insatiable. The
unacheived but desired growth may then be reflected in the financial statements 
by resorting to questionable business and accounting practices or even fraud.<br>
<br>
<b>Under pressure</b><br>
There are many reasons why companies may want to cook the books. Being a listed 
company adds to the pressure, in my opinion. Stock market performance creates 
enormous pressure on the management of companies. Stock market regulatory 
authorities require that corporate plans and information be released to the 
markets as early as possible to enable investors to react by buying or selling 
shares. For example, management has to release information about any profitable 
venture or operations it may be planning to undertake as well as any adverse 
situation it is likely to face as soon as possible. It follows that if a 
pharmaceutical company has come up with a new medicine that it expects to be the 
next wonder drug, it has to report this information to the market even before it 
gets approval from an international health regulatory body. Typically its share 
price would go up. Once the approval is obtained, this information would be made 
public and the share price should naturally edge higher. The company would also 
release information about its expected revenues and profits from this drug. If 
this information were better than what the market expected, the share price 
would climb even higher.<br>
<br>
Events don't always adhere to corporate
plans or expectations, however.
When circumstances don't go according
to plan, one should report the facts
and the impact of those facts upon the
results of operations. This is easier said
than done. Negative corporate news
brings down the share price of a company.
If the drug company referred to
above, found that the estimate of revenue
was overstated and actual revenue
is going to be lower than what was
reported in its plans, the company
should ideally report that its estimates
were higher than what it now feels it
can realistically achieve. The market
would probably react to this news and
the share price would no doubt fall. But
when management considers the
impact of this negative news on the
share price, it may unlawfully resist the
release of this negative data to the stock
market. The reasons for this could be
numerous; loss of value in their own
stock options and investments, loss of
corporate image, potential reduction in
bonuses, and reduction in credit lines
among others. Worse, even if released,
the management may decide to manipulate transactions and figures and camouflage 
the negative performance.
<br>
<br>
The resultant cover-up can start getting
bigger, the deception more malicious
and the number of players helping
to perform the accounting scam in
the organisation can multiply. Banks
and auditors can join the charade too.
The external auditor of a firm is
appointed by the shareholders of the
company and is expected to review,
check and carry out whatever tests are
deemed fit and necessary to ensure that
the financial statements of the company
are reliable. What shocked the world in
the cases of Enron, WorldCom and
some other corporate failures was the
collusion between the auditors and senior
management. Investor confidence
was shaken to say the least. Millions of
dollars were eroded from their investments.
To date, investors in Enron have
lost over $ 60 billion (RO23 billion).<br>
<br>
The US government has reacted to
these frauds by bringing in more legislation.
The Sarbanes Oxley Act of 2002
was incorporated as a means to
improve corporate governance and
make external auditors and company
management more responsible for
their jobs. The Act has set up a Public
Company Accounting Oversight
Board (PCAOB). Basically this Board
will serve as a watchdog over the
accounting and reporting related functions of the external auditors and 
management of publicly held companies.
All Public Accounting Firms (PAFs)
that audit widely held companies are
required to be registered with the
PCAOB. The PCAOB issues standards
or adopts already existing standards
issued by recognised accounting and
auditing standard setting bodies and
require that the work of the PAFs comply
with these standards. These standards
relate to auditing, accounting,
quality control, ethics, and independence among other issues.<br>
<br>
The Sarbanes Oxley Act requires
external auditors to prepare and maintain
working papers that sufficiently
support their conclusions and opinion
on the financial statements they audit,
for at least seven years. This provision
appears to be in response to the fact
that Arthur Andersen destroyed documentary
evidence while a federal
investigation was going on in the
Enron case. It appears that there is no
legislation in Oman requiring auditors
to maintain working paper files for any
specified period of time. Public
Accounting Firms may have their own
internal policies dealing with this
issue. The authorities should consider
specifying the number of years that
working paper files should be maintained
by PAFs in Oman in the interest
of preserving evidence.<br>
<br>
To ensure objectivity in the work of
the external auditors, the partner in
charge of the audit is required to
obtain a second partner review and
approval of the audit report. The audit
report to be issued under the act
requires far greater details to be communicated
to the shareholders than in
the past. The idea is to make it more
difficult and uncomfortable to specifically
say that certain tests have been
done and certain procedures have
been followed and sign off on the same
when these have actually not been
done or have been done and found to
have reportable issues that have not
been reported. The senior management
of listed companies are required
by The Sarbanes Oxley Act to commit
in the annual report and periodical
reporting that they have established
internal controls and procedures (the
body of systems designed to ensure
efficient, effective and error and fraud
free working and reporting), regularly
reviewed them and have concluded
upon their effectiveness. The audit
report has to specifically comment on
the scope of testing of the internal control
structure and procedures and
should include whether the company
maintains records to reflect transactions
and specifically whether the
records reflect the disposition of assets.
<br>
<br>
<b>Telling the whole truth</b><br>
It is not difficult to see where the
requirement to specifically comment
upon whether the company maintains
records to reflect its transactions stems
from. Enron and WorldCom both had
liabilities and significant transactions
directly relating to the business, which
were not reflected on their balance
sheets. Because of the omission of
these transactions profits were overstated
and financial statements were
misleading. WorldCom did not disclose
expenses to the tune of $3.8 billion
(RO1.46 billion) and Enron had
undisclosed losses relating to a fiveyear
period of almost $600 million
(RO230 million). The principal executive
officers and principal financial
officers are now required to certify that
the information contained in the annual
reports and periodical reporting
does not contain any untrue information
or has not omitted any material
fact, the omission of which results in
misleading financial statements. Such
certification of financial statements by
the management is still not a requirement in Oman.
<br>
<br>
Auditing conventions in the past
included the testing of the bare minimum requirements like maintaining a
complete set of accounting records and
recording all transactions. An unqualified
audit opinion meant that there
was compliance with these issues.
Today the Enrons, WorldComs, Tycos
and Parmalats have shown us that
silence on these issues does not necessarily
mean that there is compliance
with the basic conventions; it may
mean that the fact that specific comment
on these issues is not required, is
being taken advantage of. By being
silent on these issues, the management
and the auditors can take a stance at a
later date, in a court of law, that they
did not consider these issues, although
such a stance may not carry much
weight. What happens as a result of
flouting these fundamental requirements
and conventions of the profession?
Greater regulation. The Sarbanes
Oxley Act is extremely prescriptive in
nature. In addition to requiring the
auditors to specifically comment upon
whether adequate accounting records
are being maintained and whether the
transactions are properly entered in
these records, they also have to comment
on whether receipts and expenditures
are incurred in accordance with
authorisations from managers and
directors. It is akin to asking a doctor
who operates on a patient for the
removal of a tumor to sign off that he
made the incision, removed the tumor
and sutured back the patient.
<br>
<br>
The fact that a comment has to be
made upon whether receipts and
expenditures are incurred in accordance
with authorisations from managers
and directors is particularly
amusing. There is a presumption that
some officer at the bottom of the organizational
hierarchy is incurring unauthorised
expenditure without the
approval of management. This could
well be happening. But it is not this
kind of expenditure that is robbing the
investors and eroding their lifetime's
savings. In most cases it is the expenditure
and transactions made by managers
and directors in their own interests
with the full knowledge that these
expenditures/transactions are tantamount
to fraud, that need to be detected
and exposed at the earliest.
<br>
<br>
One very important provision of
the Act and one the impact of which
will be widely felt is the provision that
makes it unlawful for certain nonaudit
services to be performed by the
external auditor of a company. The
incompatible services are those of book
keeping, design and implementation
of financial information systems,
appraisal or valuation services, fairness
opinions, internal audit services,
broker, dealer or investment advisor
services among other services.
<br>
<br>
The above services, if rendered by
the statutory auditor would lead to
potential situations of conflicts of interest.
When the same firm that does the
book keeping for a company, audits
their own work, there is little chance of
their finding weaknesses or lapses and
even reporting these weaknesses to the
management or shareholders. Again,
when the firm that has designed and
implemented the financial information
system for a company finds serious
lapses in the system when auditing it,
how is it going to report this to the
management and the shareholders,
especially after it has taken significant
fees for the assignment? It is going to
remain silent on the issue in the interest
of further potential assignments
and maybe larger fees, to the detriment
of the shareholders.
<br>
<br>
The Code of Corporate Governance
(CCG) of Oman disallows an external
auditor from providing non-audit services,
which might affect their independence.
The Board with the help of the
Audit Committee is charged with the
responsibility of &quot;adopting a transparent
policy in the matter of relationship
with the external auditors, especially in
the area of award of consultancy assignments&quot;.
It would be in the interest of
the investing public of Oman, for the
Code to list out incompatible services
like the Sarbanes Oxley Act and specifically
disallow them. The list may specify
that it is not exhaustive and that individual
Boards would consider other
non-audit services on a case-to-case
basis. This will ensure that at least all
those clearly incompatible services get
identified and are not undertaken by
the external auditors.
<br>
<br>
While enhanced legislation will help
in reducing unacceptable corporate behavior, it is not going to result in a
squeaky clean corporate world. It is not
the lack of legislation or regulation that
resulted in large-scale corporate frauds. It
is sheer greed, deception and unethical
practices that lead to these failures. In my
opinion, some of the best lessons in running
successful ventures are not learnt at
professional institutes or reputed business
schools but in kindergarten and
throughout elementary school. It is the
flouting of fundamental principles such
as not to lie, rob, or cheat that has resulted
in the corporate failures and not the
failure of a complicated business model.
<br>
<br>
In an effort to be proactive in avoiding frauds some which can be so massive as 
to affect the entire economy, it would be appropriate for the Code of Corporate 
Governance of Oman to require that listed companies have a code of ethics for 
senior management and the Board. At periodical meetings the code of ethics 
should be reiterated and commitment to it should be affirmed. Some unethical 
acts are committed because of a feeling that it really doesn't matter. Once 
there is a corporate ethical code in place it will show that ethical behaviour 
does. After all, it is easier to break unwritten rules than to break written 
ones.</font></p>

