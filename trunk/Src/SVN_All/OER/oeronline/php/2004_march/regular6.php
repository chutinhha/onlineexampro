marketing to an agent, to gain better results we chose to start
our own company there to take charge of warehousing, marketing
and distributing Raha branded products. Similar
warehousing and sales offices were set up in Kuwait and
Saudi Arabia followed soon afterwards. Today, around 55 to
60 per cent of the goods Poly Products produces are exported,
mostly to the GCC.
