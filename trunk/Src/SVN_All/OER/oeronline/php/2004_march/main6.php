            <b><font face="Verdana" size="1">
                    It's that time of the year again and the Muscat Festival is 
            back. Organisers say its bigger and better than ever. Are they 
            telling the truth? OER reports</font></b></p>
                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="im8.jpg" align="left" hspace="5" width="90" height="107">Dogged 
                    initially by the threat of a winter downpour, the 2004 
                    Muscat Festival finally got off to a splendid start with a 
                    lavish opening ceremony that set the tone for Oman's annual 
                    cultural blowout. <br>
                    <br>
                    A dazzling display of choreographed dances and a multimedia 
                    show, complete with high-voltage pyrotechnics and special 
                    effects, wowed the audience of government dignitaries, 
                    ambassadors and invited guests. It also brightened hopes 
                    that the month-long extravaganza would live up to its 
                    billing as a big draw with GCC travellers and other foreign 
                    tourists, thereby helping perk up a hospitality sector badly 
                    mauled by the Iraq war and other regional crises. <br>
                    <br>
                    The main organizer, Muscat Municipality, is predicting a 
                    record turnout surpassing the two million odd visitors who 
                    patronised last year's edition of the festival, which runs 
                    from January 19 to February 20. Earmarking a hefty RO 11 
                    million for this year's celebration, it has launched a 
                    high-profile advertising drive on several leading Arab TV 
                    channels and other regional media. The Directorate General 
                    of Tourism has weighed in as well with its own promotional 
                    drive touting Oman's greatly liberalised visa regime and the 
                    plethora of offers and incentives dished out by the hotel 
                    industry.<br>
                    <br>
                    In a departure from the practice of conducting the Muscat 
                    Festival just before the <br>
                    &quot;Mother of all Festivals&quot; - the Dubai Shopping Festival (DSF), 
                    officials here decided to have this year's edition in 
                    parallel with Dubai's mega-blowout in the hope of cashing in 
                    on the latter's extraordinary success. <br>
                    <br>
                    Dubai, which attracted a record 4.5 million visitors last 
                    year, is seen as an important lodestone for international 
                    tourists - one reason why Oman's tourism industry is 
                    increasingly gearing its promotional efforts to this market. 
                    Nationals from a number of Western, Far Eastern and South 
                    American countries are not required to obtain separate visas 
                    to visit the Sultanate if they bear an entry stamp or visa 
                    for Dubai. Oman has pledged to pull out the stops in an 
                    effort to facilitate tourist travel, waiving fees for such 
                    nationals and keeping border formalities to the minimum. <br>
                    <br>
                    By overlapping the 2004 Muscat Festival with the hugely 
                    popular DSF, the Sultanate is banking on its appeal as an 
                    authentic Arabian destination to sway tourists into making 
                    the short drive or flight into Oman. Furthermore, officials 
                    are hopeful of a strong influx of UAE and other GCC 
                    nationals looking for a winter break especially over the Eid 
                    al Adha holiday. The festival's emphasis on Oman's vibrant 
                    heritage and culture, as reflected in the daily fare of 
                    traditional Omani folk dances and heritage displays, greatly 
                    appeals to GCC and Arab nationals. <br>
                    <br>
                    But not everyone is upbeat about what the 2004 Muscat 
                    Festival has in store in terms of a successful outcome. The 
                    private sector's role in this year's féte has been 
                    conspicuously low-key. Many of the corporate sponsors appear 
                    to have grudgingly come on board, skeptical about seeing any 
                    healthy returns on money ploughed into the extravaganza. <br>
                    <br>
                    At a press briefing held early in January, Municipality 
                    officials did little to mask their displeasure over the 
                    private sector's tepid involvement in the festival. They 
                    criticized the private sector's &quot;purely commercial&quot; approach 
                    to the festival, rather than supporting it as a national 
                    enterprise that aims to enhance Oman's image and prestige 
                    regionally and internationally. For its part, the private 
                    sector says it has misgivings about the vision and appeal of 
                    the festival in pulling in tourists and keeping the cash 
                    registers ringing. <br>
                    <br>
                    Some of the acrimony has its roots in last year's festival 
                    when the slovenly handling of some high-profile events, 
                    blamed on a private organiser, somewhat marred an otherwise 
                    splendid bash. <br>
                    <br>
                    Determined to show that it can hold its own, Muscat 
                    Municipality is largely going it alone in the organisation 
                    of the 2004 festival. While this year's offerings may lack 
                    some of the razzmatazz of last year's mega concerts and 
                    international sporting events, the Municipality says it has 
                    put together a winning programme, nonetheless. As with 
                    previous editions of the festival, this year's extravaganza 
                    is also conceived as a family entertainer with added 
                    emphasis on Oman's cultural, social and historical heritage.
                    <br>
                    <br>
                    The festivities are being hosted at three key venues in 
                    Muscat - Qurum Natural Park, Festival Plaza (Al Khuwair) and 
                    Seeb Beach Park. A new facility at Qurum Natural Park is 
                    Marah Land (a.k.a City of Joy), a newly revamped amusement 
                    park set on 107,000 sq metres of land. In addition to the 
                    giant wheel, merry-go-round, whirligig, flying horses and 
                    roundabouts, it also has go-karts, dune buggies and 
                    inflatables. Other features at the park are the Bahja 
                    Children's Village, the Aqua Circus, the Bubble Man, Snow 
                    Village, and some Jurassic era attractions. Daily multimedia 
                    shows and weekend concerts are the highlight of the park's 
                    Lakeside Theatre.<br>
                    <br>
                    At the Festival Plaza, the accent is more on international 
                    folk dances, heritage displays and trade shows. The 
                    Amusement Park, with its assortment of games and rides, is a 
                    big draw with youngsters with an appetite for some 
                    heart-stopping thrills. <br>
                    <br>
                    Sports enthusiasts would do well to visit the Seeb Beach 
                    Park on weekends to watch international teams slugging it 
                    out in various championship events. Concerts featuring 
                    well-known Arabic, Asian and Omani bands are planned as 
                    well. <br>
                    <br>
                    This year's festival also comes with a large dose of 
                    educational events. Oman International Exhibition Centre, 
                    Seeb, is hosting a series of courses and competitions 
                    directed at children. A programme of lectures and literary 
                    evenings has also been launched at the Sultan Qaboos Grand 
                    Mosque. Several leading hotels and shopping malls are 
                    pitching in as well by staging special events under the 
                    auspices of the Muscat Festival.<br>
                    <br>
                    There is little doubt that the festival will be a 
                    crowd-puller, as reflected in the strong turnout on the 
                    opening days of the extravaganza. The challenge, however, is 
                    to ensure that the festivities possess enough allure to keep 
                    the crowds turning up night after night.</font></p>
