            have to constantly try out something new. This Ramadan, for 
            instance, we had the Waleema scheme, which was the first time such a 
            scheme was launched in Oman. It met with tremendous success. 
            Similarly, we are credited with a lot of firsts - 24-hour road 
            service, Faeeda Car, hotline etc. - in Oman. <br>
            <br>
