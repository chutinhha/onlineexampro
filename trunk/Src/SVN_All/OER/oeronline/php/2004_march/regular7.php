            like Oman, we have 22 showrooms, 14 workshops, five quick service 
            centres, 60 electronics outlets, 600 spare parts outlets and over 
            1,000 tyres outlets. Out of these, we opened three new showrooms, 
            two service centres and one quick service centre in 2003, and we are 
            continuing making such investments. <br>
