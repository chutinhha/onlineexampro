            <br>
            <b>What would you do to bring out the best in your employees? </b>
            <br>
            To bring out the best in employees, delegate both responsibility and 
            accountability. It does not work if you delegate only one. Also, 
            treat them fairly and reward them handsomely when they perform well.
            <br>
