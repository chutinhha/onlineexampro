            <b>What drives OTE? </b><br>
            Four important factors have helped to put OTE on the top: <br>
            <b>a) Customer care and focus:</b> The main thing for any car 
            company is to take care of their customers, and that's what we place 
            a lot of emphasis on. For instance, we have 24-hour roadside 
            assistance anywhere in Oman and the UAE, which entails a free 
            pick-up for the customer and his car at any point in time. Mind you, 
            it's a very expensive proposition for us but, at the same time, 
            means peace of mind for our customers. Similarly, we have an 
            excellent complaint response programme, which is monitored by my 
            office. If any complaint is not addressed in 48 hours, it is 
            escalated to me, and this acts as a deterrent. <br>
            <br>
