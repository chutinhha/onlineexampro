            <b>Can you briefly sum up your preferred management style... </b>
            <br>
            Without a doubt, you could call me a compulsive delegator. In fact, 
            one of the first things I did was to make my general managers wholly 
            responsible for both revenues and costs in their franchise. 
            Otherwise, I would land up with the impossible task of doing the job 
            of everyone in the company except mine! It has been known to happen!
