          <p align="left"><font face="Verdana" size="2"><b>OER: You indicated at your result presentation press conference that you are looking at seeking legal redress from the firm that audited the books of one name in particular who owes you a lot of money.&nbsp;</b><br>
          <br>
          <b>AH:</b> I’m not indicating, I’m telling. We’re talking to our lawyers. As I said in the press conference, we don’t treat provision in this bank as a loss. Provision is an accounting arrangement that we have to do to report to our shareholders and investors and interested parties and regulators and so on. But as a management team we see provisions as something we have to go back and get. That means we will use every managerial step, every legal step possible, to get back our money from whoever we should get it from. If we were misled by bodies that we should not be misled by, and the clients who may have the money wherever they have it, we’re going to certainly try and find it.&nbsp;<br>
          <br>
          <b>OER: Most of the major banks have been stung by this one borrower.&nbsp;</b><br>
          <br>
          <b>AH: </b> I would say all the banks, local and foreign.<br>
          <br>
          <b>OER: We understand this person borrowed in excess of RO50 million from the various banks in total. It seems banks had a false impression of the size of his trade. But we understand that the amount he was borrowing far exceeded what would reasonably be needed to run the business he said he was involved with. Do you think that the banks should have asked more questions when lending such huge amounts of money to one individual?</b><br>
          <br>
          <b>AH:</b> If it’s the name that I think you’re thinking of, it has been on our books since 1989. So we’ve built that relationship. New people who came, as late as 2000 actually, judged the name (to be) good. Because the name is probably going to be sub judice in a few weeks’ time, I wouldn’t want to comment more on it. Except to say yes, in hindsight you can see all kinds of issues.<br>
          <br>
          <b>OER: So more questions should have been asked?</b><br>
          <br>
          <b>AH: </b> Obviously more questions should have been asked. But when you look at the development, it’s not something that happens to you one day.&nbsp;<br>
          <br>
          <b>OER: You’ve said before that too much lending is done on a ‘name’ basis in the region. In other words, ‘wasta’ works in banking just as it does in other spheres of Arab society. Is this a cultural practice that needs to change, not only in banking, but in business in the Middle East generally?</b><br>
          <br>
          <b>AH:</b> I have worked in the region for the past 20 years, and certainly done business in all the GCC countries, and it is a cultural practice. You think of one big name in Saudi Arabia, in Kuwait and anywhere else, and you see a family behind it and generally still a man behind it. So it’s a natural phenomenon. It’s just less in the West.&nbsp;<br>
          What we have to do is now move to the place where these big names are now big businesses. Us banks must get across the message to them that ‘you’re no longer running just your private business, you’re taking money from shareholding institutions where people don’t know you, and these are shareholders who management and boards of banks are responsible to’. We want financial statements. Luckily, the Central Bank of Oman has led the way in the region and has now insisted that by the 31st of March, anybody who is borrowing from a bank RO250,000 or more must produce an audited financial statement, whether you’re an individual of a company or whatever you are. Now that’s an excellent move forward, and that’s going to help the banks. I think more and more central banks should do this.<br>
          Even then, we as bankers and lenders must obviously become a lot more judicious. We’re going to have to probably say a lot more ‘no’s’ than we have in the past. And that’s already happening in
          NBO. I’ve said very clearly that we are weeding out names that no longer fit the NBO risk profile.&nbsp;<br>
          <br>
          <b>OER: Do you believe this result marks the bottom of the cycle, and that NBO will return to profit growth this year?</b><br>
          <br>
          <b>AH:</b> Yes.<br>
          <br>
          <b>OER: Do you expect NBO to post a net profit for full-year 2002?</b><br>
          <br>
          <b>AH: </b> Yes, we expect to post a net profit for the year.<br>
          <br>
          <b>OER: Does that mean a return to dividend payments this year?</b><br>
          <br>
          <b>AH:</b> I would hope that would be the case. But I must also put a caveat there. UBS was going along wonderfully, and then September 11 happened. In the fourth quarter, Swiss Air went bankrupt. JP Morgan Chase and CitiCorp were doing wonderfully well, then Enron happened, then Argentina happened. We were going quite well with some clients here in 2001…things turned bad, we had to provide and we’re looking at legal steps in quick succession. So I say ‘yes’ to those questions. That’s our plan, our business plan looks that way, but I must put in a caveat.<br>
          <br>
          <b>OER: How do you feel about the losses, and do you personally accept any of the blame for them?</b><br>
          <br>
          <b>AH: </b> As somebody who believes in the capitalist system, it’s a terrible place to be in where you do not pay dividends to your shareholders, (especially) when you’ve had a consistently good record and they expect you to pay dividends. And I hold 140,000 shares in the bank, I am one of those shareholders. So I feel personally as a professional terrible about not being able to pay a dividend, and personally as an investor in a terrible position. So yes, no doubt about it. And frankly this position is not where I expect to be at the end of this year. We have to get back to a place where we’re producing profits, and we hope dividends as well.&nbsp;<br>
          <br>
          <b>OER: So you shoulder responsibility for the losses?</b><br>
          <br>
          <b>AH: </b> I am the chief executive officer of this bank. The first place, and very often the last place, where that responsibility has to rest, is in my office. I have to take that personal responsibility up front. Funnily, when you’re leading an
          organisation, all the blame is yours. As much as possible I share the credit.<br>
          <br>
          <b>OER: Have you considered hanging up your boxing gloves and stepping down as CEO?</b><br>
          <br>
          <b>AH:</b> That would be, frankly, the easiest thing to do. I’m pretty sure with time I could get another job somewhere else. I’m willing to work hard and I think I’m decently intelligent. Somebody will, after time, employ me. And the board actually can make that call. I am employed by the board. You’re asking me my position – I have never left a situation where the circumstances were difficult. I don’t run away from problems. In fact, when I took over the bank (in 1991), the bank was effectively bankrupt. I was told by many of my colleagues in the banking industry that I was stupid to take the job. But the challenge was to take the bank from a state of bankruptcy. And now we have a net worth of over RO105 million, six years of paying dividends. Certainly we now have a problem, a problem that is much smaller than the one that I inherited when I took over the bank, it certainly is not the time to run away from it. I think I’m still in good health and we will work this out.<br>
          It’s the board’s call. I’m sure they will consider whether the experience we’ve given over the last 10 years, and especially last year, should be just put away or used to the benefit of the
          organisation.<br>
          <br>
          <b>OER: A few days before you announced the loss you appeared on the front cover of another business magazine, which named you the best CEO in Oman. Do you agree with that assessment?</b><br>
          <br>
          <b>AH:</b> First of all what I will say is I didn’t choose myself. I understand that over 100 senior people made that decision. I’ll graciously thank whoever gave it. However, one of the things that we must remember is that it’s not easy for a CEO to go and face the press, the brokers, the analysts, the very day after the board makes the final decision on a loss. While we have stood up in good times we have also stood up in bad times, so maybe they know what they were saying.<br>
          <br>
          <b>OER: Thank you. </b> </font>
          </p>

