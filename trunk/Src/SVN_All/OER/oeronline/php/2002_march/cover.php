                    <font face="Verdana" size="4" color="#FF0000">For Better or Worse</font>

                    <p><font face="Verdana" size="1"><b>National Bank of Oman (NBO) shocked the market last month when it reported a RO6.4 million net loss for full-year 2001. The loss was due to a massive RO30.5 million provision for bad loans. Matthew Brockett caught up with NBO Chief Executive Aubyn Hill to discuss the current situation and the bank’s future&nbsp;</b></font></p>

                    <p>&nbsp;</p>

