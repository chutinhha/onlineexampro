          <p align="left"><font face="Verdana" size="2"><b>OER: Could you identify the main reasons why NBO has had to make a RO30.5 million provision for loan losses?&nbsp;</b><br>
          <br>
          <b>Aubyn Hill: </b> Just like the stock market, the banking industry does not operate outside the economy. Since the end of the first quarter in 1998, when oil prices hit a low of below US$10 (per barrel), the non-oil economy has had a very difficult time. The stock market, if you look at the trend line, has been one-way downwards. It’s the same situation that has now come to hit us. After three, four years of a slow economy or slow growth in the non-oil economy, and in some sectors no growth at all, you find that we’re making these provisions. Now that doesn’t preclude the fact that some decisions should have been made better. We can’t avoid that issue. But in a good and growing market, even weak decisions can survive. In a consistently down market, even good decisions face difficulty. So that’s the background.<br>
          Now why have we made this (provision)? Two names are responsible for more than 50 per cent of this figure. One is a local name - by law I can’t talk about names in particular, but I’m sure the market is so well informed that they’ll know who we’re talking about. Another is an oil-related name that has not done well. It has business in the three countries in which we operate, in Egypt, in Oman, and in Abu Dhabi.<br>
          You have to take about RO4 million out of that RO30 million for consumer provisions. Out of that (remaining) RO26 million, much more than half of the balance is from those two names. The rest, we have some smaller names here, they add up. And of course we have the devaluation in Egypt.&nbsp;</font>
          </p>
          <p align="left"><font face="Verdana" size="2"><b>OER: With hindsight, was the bank’s expansion into Egypt a mistake?</b><br>
          <br>
          <b>AH:</b> We decided in 1994/95 to expand in Egypt. Those were good years. In fact, 1995/96, Egypt had a balance of payments surplus of US$19 billion. The Egyptian pound was increasing against the US dollar. Now in an economy like that, why wouldn’t you grow your business? But when it changes, you obviously have to pay the price.&nbsp;<br>
          We made a lot of money and we will continue to. We are committed to Egypt, we’ve just changed the direction of the business. The Cairo market has 12.5 million people. If we just cherry pick the best of the consumer business in Egypt…just under a million clients, we have all the business that we need. And I think that’s a business we understand and know thanks to our operations in Oman.&nbsp;<br>
          <br>
          <b>OER: Should NBO and other Omani banks have been more cautious when lending money during the 1990s?</b><br>
          <br>
          <b>AH: </b> One of the things I keep saying to my board, in taking obvious responsibility as we go forward, is that it’s useless to explain a bad loan. A bad loan is a bad loan. It’s useless for me to tell you what were the circumstances when we made a bad loan. Because I can tell you, no honest person, okay, no sensible person puts a bad loan on the books of a bank. Every loan you put on is good when you put it on.<br>
          The system we have in NBO is one where a number of people look at loans at different levels in the bank. So a number of people…thought that these loans were good. This is not new money we’re talking about, these are clients we know. Circumstances change on them.&nbsp;<br>
          We should also put this year in context. We’ve had eight or nine years with great results, paying in six years RO47 million in dividends. So you know we’ve had a good run. This bank in the past 10 years has grown from an asset book of RO243 million to assets at the end of 2001 of RO951 million. That kind of geometric growth in your asset book means that you’re doing business, and in doing that business you’re taking risk. Lending money is a risky business. We’ve had a bad year this year. (But) once you put that in perspective, we have made obviously a lot of good loans.&nbsp;<br>
          <br>
          <b>OER: What measures have you taken to ensure that the bank does not continue to expose itself to bad loans?<br>
          </b><br>
          <b>AH:</b> Everything that you experience in life, whether it’s good or bad, you should learn from it. We think we’ve leant. We obviously have to monitor our clients much more carefully. It’s the basic stuff. The first thing is the account officers who handle credit, they have a completely different directive as to how they must handle it. It must be cash-flow based, we must have significant collateral, and there’s some business we don’t want. We have to be a lot more selective.&nbsp;<br>
          The biggest provision we had this year was audited by one of the biggest name firms in the world, not for one year but consistently for five or six years. About four or five years ago, we had that audit firm telling us clearly what the net worth and sales of the owner of that company was. And subsequently they’ve done all of the audits. Well when we sent in another audit firm to do the check after the problems happened, they said ‘look, there are questions here. How could they not do
          ABCD?’ So even though we have the outside firm checking, we have to be doubly careful.<br>
          But still, in the final event, you are lending money. It is risky. All of it will not come back to us. What we have to do is make sure we really rapidly and significantly reduce the amount that we have to take provision for.</font>
          </p>
