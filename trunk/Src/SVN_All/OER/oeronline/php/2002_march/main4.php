                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Dial "P" For Privatisation</font><p><font face="Verdana" size="1"><b>The Sultanate has approved plans to privatise Omantel this year. Now the race is on to draft new telecommunications laws and set up an independent regulatory authority to regulate the market. Nayl D'Souza reports&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2002_march/14A.jpg" align="left" hspace="5" vspace="5"></font><font face="Verdana" size="2">It's official. Omantel is to be privatised. Last month the Government announced that it will divest 40 per cent of the company to a consortium of Omani and foreign investors and release an additional nine per cent of Omantel's equity to local pension funds. The Government will retain the remaining 51 per cent stake.&nbsp;<br>
                    The scheme aims to ensure that the Sultanate opens up its market to world trade and attracts strategic investors from abroad to help expand the nation's network and keep pace with technological change.<br>
                    Omantel's privatisation has been in the offing for some time. But despite of the successful privatisation of airports and electricity, the process stalled.<br>
                    Economy Minister Ahmed bin Abdul-Nabi Macki said the global economic downturn in 2001 hampered earlier plans to sell Omantel. He also said the process was slowed down by some necessary re-organisation of the Telecommunications Ministry. But the Minister is now confident that the project is back on track.&nbsp;<br>
                    "We are going to make a fresh launch to our privatisation programme to sell Omantel. We find the global market now much more receptive than it was last year," he said.<br>
                    Industry officials welcomed the recent appointment of new Omantel President Mohammed bin Ali Al Wahaibi as another step towards speeding up the sell-off, which had been put on hold since 1999.&nbsp;<br>
                    "I'm sure that the Government is taking the issue more seriously than ever and Omantel is certainly readying itself for change with a new President at the helm," confirmed a source close to Omantel's Board of Directors.&nbsp;<br>
                    The Government has also announced it is seeking to appoint consultants to give advice on quickening the privatisation of Omantel. Jill Hills, Professor of Telecommunications at the University of Westminster in the United Kingdom, said: "I expect that Omantel will need to go through rapid organisational restructuring even if there isn't privatisation by the end of the year".&nbsp;<br>
                    As part of its accession to the World Trade Organisation (WTO), Oman has made several commitments to the WTO that include ending the Government's exclusivity for providing mobile phone and Internet services before 2003.&nbsp;<br>
                    "Before January next year, Omantel's cellular license will be subject to accounting separation at the very least to eliminate unfair cross-subsidy," said Professor Hills. "As per the WTO agreement, an independent regulator must be set up. However, it's a chicken and egg situation. You cannot have a licensing regime unless there's a regulatory law, but you cannot have a regulatory law unless a regulator exists."&nbsp;<br>
                    Mazin Abdullah Al Taie, advisor to the Minister of Transport and Communications, said an industry watchdog was likely to be established before the middle of this year. The main priority for such an authority would be to regulate pricing. "Aside from price capping and rebalancing, an independent regulator will need to be in control of opening up the market, managing competition, giving technical standards, managing numbers and configurations within the national network, managing and allocating the spectrum of communication frequencies, issuing licenses, monitoring the interconnection between operators, and ensuring there is customer care and satisfaction together with adequate user information," Al Taie said.&nbsp;<br>
                    An Omantel manager also felt that the main reason for the privatisation must be to improve Omantel's overall performance. "As a state-owned monopoly, customers are not always the main priority of our business," he conceded.&nbsp;<br>
                    Protecting every citizen's right to access telecommunications will be a major concern for the regulatory body. Areas such as Muscat, Salalah and the Batinah Coast region will be the most strategic business locations for additional players in the market while the overall network may not be altogether profitable, according to one industry chief. Despite this, financial analysts are upbeat about the potential of the telecommunications sector.&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_march/15N.jpg" align="right" hspace="5" vspace="5">"Currently, Omantel operates in a comparatively profitable sector and I believe that it makes quite significant profit," said Mahesh Chandra, a fund manager for Al Mawarid Securities Co. Omantel, which has never published its profit figures, has invested over RO70 million (US$182 million) in the past two years on expansion of its network and additional value added services.&nbsp;<br>
                    Showing confidence in the opportunities in the local market, ministerial advisor Al Taie said: "There is a good telecom structure and a large network covering good areas. The penetration rate for Oman is one of the lowest in the GCC. There are about nine fixed lines per 100 inhabitants while the cellular subscription is around 15 per cent, so there's plenty of potential to build custom for a second operator."&nbsp;<br>
                    The latest official figures show that the number of fixed phone subscribers in the Sultanate has increased to 234,489 from 226,353. The number of Internet subscribers stands at 45,000.&nbsp;<br>
                    The Government has also indicated that it may later offer a stake in Omantel to subscribers as a token for their loyalty to the company. Experts say an international public subscription at a later stage could substantially boost the Muscat Securities Market (MSM) if the stock is listed locally.&nbsp;<br>
                    "If Omantel were to float, it would be a good company to track and we'd assume it would do very well for investors," said one share broker. "However, it wouldn't radically change the MSM's overall performance. It would simply rank among other leading services companies on the bourse such as Salalah Port Services and Oman Aviation."&nbsp;<br>
                    However, as the Sultanate turns its attention to Omantel's privatisation, there are concerns that the call to large foreign investors may not be answered. "It is dubious whether the big global telecom giants will invest as most are up to their neck in debt," one analyst said.&nbsp;<br>
                    This concern that few buyers will come forward is shared by a local banker, who said: "The privatisation of the telecom sector is very complex and all the relevant legislation will have to be decided upon quickly if the Government is to meet its target this year and promote confidence."&nbsp;<br>
                    Besides Omantel's privatisation, the Government is in the process of introducing a regulatory authority to improve postal services and studying the scope for private participation in the postal services sector. A panel of senior government officials are also in talks to introduce new regulations governing the electronic media such as the rapidly growing Internet.&nbsp;</font></p>

