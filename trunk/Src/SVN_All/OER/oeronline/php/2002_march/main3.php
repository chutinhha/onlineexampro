                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Experts Bank on better Transparency to Boost Muscat Securities Market&nbsp;</font><p><font face="Verdana" size="1"><b>Reeling from an almost total lack of investor confidence, the dazzling heights of 1997 are a distant memory for the battered Muscat Securities Market (MSM). Experts say a recovery hinges on better company disclosure. Saleh al-Shaibany reports&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2002_march/48.jpg" align="left" hspace="5" vspace="5" width="225" height="149"></font><font face="Verdana" size="2">The MSM plummeted 24.4 percent last year to 152.08 points, making it the worst performing stock exchange in the Gulf. Clearly, recent reforms have failed to reverse the negative investor sentiment that is weighing on the Omani bourse.&nbsp;<br>
                    <br>
 Economists say the market is unlikely to turn the corner until even greater transparency is introduced.<br>
                    "Companies need to invite analysts and the press for quarterly meetings,” says V. Ramakrishnan, Head of Investment Banking at the National Bank of Oman (NBO).&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_march/49A.jpg" align="right" hspace="5" vspace="5" width="150" height="192">“This will not only generate awareness and improve transparency but also increase investor confidence. National Bank of Oman has been arranging for such meetings since the first quarter of last year. The board and management must present a detailed account of their company's state of affairs during the annual general meetings. They also must issue press releases on major corporate events whenever required for the benefits of investors. Such actions will further improve transparency, " he says.&nbsp;<br>
                    The bourse regulators, the Capital Market Authority (CMA), beefed up disclosure rules last year, but analysts say more needs to be done.&nbsp;<br>
                    "The CMA has failed to properly investigate allegations that some company directors used their positions to access privileged information and caused the market to plunge," an analyst at a brokerage company says.&nbsp;<br>
                    "Insider trading needs to be looked at closely and offenders must be publicly identified so investors can make up their own minds. For example, the merger of Industrial Bank of Oman and Bank Muscat leaked before the banks actually announced to the public their intention," he says.&nbsp;<br>
                    Bank Muscat, Oman's largest bank in terms of assets, strengthened its position by merging with Industrial Bank in December last year. But the bank, like most blue-chip firms trading on the MSM, saw its share price fall more than 50 per cent in 2001.<br>
                    Economists are blunt in their assessment of some analysts who failed to warn investors of possible financial mismanagement of fraudulent firms.&nbsp;<br>
                    "Some who call themselves analysts are incompetent or worse, or just simply scared of their positions," one economist says.&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>INVESTOR CONFIDENCE SHATTERED&nbsp;</b></font><font face="Verdana" size="2"><br>
                    Some experts say a turnaround in the market can only happen if some of the listed companies change their corporate leadership.&nbsp;<br>
                    "Some of the firms trading on the MSM are run by the same board members for the last 20 years,” says Dr Fahim Al Marhubi, Head of the Economics and Finance Department at Sultan Qaboos University.<br>
  “These companies need to appoint new directors who will push for new business developments. Investors, on the other hand, should forget the market boom of 1997,” he says. “The market then was pushed up by speculation fuelled by insider trading. They looked at the stock market as a quick way to make money…they still do now but this attitude has to change if the bourse is to recover."&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_march/49B.jpg" align="left" hspace="5" vspace="5" width="150" height="205">The MSM, which opened in 1989, gathered momentum in 1996. The index rose by 140 per cent to 480.58 points in 1997 to become the most advanced emerging market in the region. But the bubble burst a year later when the index spectacularly lost 52.46 per cent in December 1998, plunging to 228.47 points after reaching an all-time high of 509.84 points in February the same year.<br>
                    Al Marhubi says the reason that the market is performing so poorly today is that investors have completely lost confidence in the bourse, fearing a repetition of the slump of 1998. “They now go for short-term gains and sell as soon as the stocks go slightly higher," he says.&nbsp;<br>
                    The MSM staged a moderate but short-lived rally in November 2000 after the Government intervened by injecting RO50 million into the market. The index rose more than 12 per cent in a week but started a gradual downward trend early in 2001 as fears over disclosure again dented investor confidence.&nbsp;<br>
                    Bankers say the Government should not have intervened but let investors learn from the 1998 crash.&nbsp;<br>
                    One senior banker says well-informed investors reacted quickly to the Government’s intervention, using inside information about which stocks the Government would buy before the State had spent the money. “The regulators then realised that they had a problem in their hands and quickly revised transparency rules and started reforms," he says.&nbsp;<br>
                    "The Government action to pour money into the bourse was well-meaning but this decision suggested that the stock exchange was in serious trouble. I think the Government, like investors, has learned its lesson and it must in the future concentrate on monitoring the bourse and not bail it out.”<br>
                    In January last year, CMA chief Yahya Al Jabri told reporters the Government had ruled out further intervention, saying it was "a once-only measure and investors should learn" from the experience.&nbsp;<br>
                    The MSM reached one of its lowest points when it plummeted to a seven-year low of 146.3 points in December last year.&nbsp;<br>
                    "The message is simple," Al Marhubi says. "The CMA's disclosure rules are commendable but they will remain paperwork if they are not implemented in practice by prosecuting violators to deter future offenders. This action will encourage investors to go for long-term investment and even attract more foreign capital."&nbsp;<br>
                    In a rare crackdown last year, a court sentenced a former senior government official to six years in prison for committing fraud on the official bourse.&nbsp;<br>
                    As OER went to press, the MSM was wallowing at 157 points.</font></p>

