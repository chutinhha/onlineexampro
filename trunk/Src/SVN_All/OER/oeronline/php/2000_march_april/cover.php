                  <td width="100%"><img border="0" src="../../images/2000_march-april/wto_1.jpg" align="left" hspace="5" vspace="5" width="255" height="198"><font face="Verdana" size="4" color="#FF0000">Oman and the WTO -<br>
                    the real impact</font>
                    <p><font face="Verdana" size="1"><b>Much has been reported and rumoured in the Gulf media recently regarding the Sultanate's planned membership of the World Trade Organisation. To bring OER readers the real story, WTO Director-General Mike Moore talked exclusively to Polly Stewart about the international trade organisation and Oman's potential place in it.</b></font></p>
                    <p><font face="Verdana" size="2"><b>OER:</b> "What do developing Middle Eastern countries like Oman, with relatively open economies and stable political systems have to give to the WTO?"&nbsp;<br>
                    <br>
                    <b>MM:</b> "The World Trade Organization has 135 members at present. Members from the Middle East contribute to the universality of the multilateral trading system. With Egypt, Kuwait, Bahrain, Qatar, and UAE already members, Jordan having recently completed its accession negotiations, and Saudi Arabia and Lebanon in the process of accession, it is only befitting that Oman should soon join the organization which establishes the ground rules for international trade."<br>
                    <br>
                    <b>OER:</b> "What would Oman gain from WTO membership?"<br>
                    <br>
                    <b>MM:</b> "Oman - a relatively open and small economy - has much to gain from its external commercial trade, technological and investment relations being conducted within a non-discriminatory and liberal rules-based system. The WTO provides such rules, and membership in the WTO will allow Oman to participate, on an equal basis, in the further elaboration of such rules in future multilateral trade negotiations. WTO membership also offers Oman access to the markets of other Members on Most Favoured Nation terms, and the possibility to improve these terms through multilateral negotiations in the future. Most importantly, membership of the WTO will give Oman access to the WTO's dispute settlement procedures to ensure against unjustified actions taken against Oman's interests by its trading partners, big or small."<br>
                    <br>
                    <b>OER:</b> "What criteria does Oman have to satisfy in order to gain membership?"<br>
                    <br>
                    MM: "In order to join the WTO, an acceding government must demonstrate that it is capable and willing to comply with WTO requirements and rules. All acceding countries find that this implies a major effort in the area of legislation and enforcement. New laws have to be drafted in previously unregulated areas (for example, intellectual property), existing legislation must be modified (e.g. customs procedures) and non-compatible measures and practices must be done away with (e.g. import quotas and bans). In addition, an acceding government must establish its terms of entry with regard to trade in goods (i.e. tariff concessions), and its market opening commitments in respect of trade in services."<br>
                    <br>
                    <b>OER:</b> "How long does the membership review/admittance procedure take?"<br>
                    <br>
                    <b>MM:</b> "Each accession to the WTO is a process of negotiation, and as such is not subject to any predetermined deadline. As a general rule, due to the comprehensiveness and complex nature of these negotiations, they require minimum two years to complete, but can easily take more time if the market access negotiations on goods and services prove difficult, or the legislative machinery in an acceding country is not working as fast and efficiently as one might have wished."<br>
                    <br>
                    <b>OER:</b> "Is Oman likely to gain membership of the WTO? If so, when is this likely to be formalised?"<br>
                    <br>
                    MM: "Oman is very close to the end of its accession negotiations. More than 95% of the work has been done, and the remaining issues can be sorted out in a matter of weeks rather than months. When the negotiations in Geneva have been completed, Oman will be invited to formally ratify its accession package. It is only after this procedural step has been completed, notified to the WTO, and 30 days have been allowed to lapse, that Oman actually becomes a member of the WTO. Given the advanced stage of Oman's accession, however, I am certain that it will happen this year."<br>
                    <br>
                    <b>OER:</b> "Oman's two major sources of national revenue come from the finite oil and gas industries. Pegged as the next biggest potential earner for the Sultanate is its developing tourism industry. How will WTO membership help Oman develop such a national resource?"<br>
                    <br>
                    <b>MM:</b> "Like other countries, Oman has a choice between developing its tourism industry relying exclusively on domestic capital, or with foreign investment. It goes almost without saying that development will be faster with the participation of foreign investors, and the returns will be even higher when foreign partners bring their tourists with them. For foreign investors, no matter in which sector or activity, the protection of their investment by rules is paramount. It is no accident, therefore, that conditions governing investment in Oman's tourism industry, and the supply of tourism services in Oman, are included in Oman's accession package."<br>
                    <br>
                    <b>OER:</b> "Some media in Oman have reported a number of major disadvantages in Oman joining the WTO, such as Oman being forced to not discriminate between its own products and those imported from WTO member countries which is seen as a threat to locally produced goods. Are there really any medium to long term potential negative implications for a developing country such as Oman attached to WTO membership? What are the short-term implications?"<br>
                    <br>
                    <b>MM:</b> "As a member of the WTO, Oman will still be allowed to protect its industries by tariffs, as long as these are set within the limits that Oman have negotiated in its accession package. As for the less transparent and hidden forms of protection, such as import quotas and bans or discriminatory "standards" and sanitary or phytosanitary measures, these will have to be eliminated. Oman may still apply strict requirements for reasons of public health, safety, or to protect the environment, or take all the actions sanctioned by the WTO to protect its industries from unfair trading practices, such as dumping. The important point is that such rules are then applied to safeguard the well-being of Oman's population, and not for the unjustified benefit of local producers at the expense of the consumer.<br>
                    <br>
                    Oman's economy is quite open. Therefore we would not expect that Oman should endure a painful economic transition as a result of accession to the WTO, although let's face it, no structural adjustment in quest of a more efficiently running economy is completely painless.&nbsp;<br>
                    <br>
                    Some people may argue that without heavy protection it will be difficult to establish new industries. The answer to such an argument is that industries which are not profitable in the long run without massive protection are evidently not viable, and should not have been established in the first place. It is very important to examine these issues from the point of view of the economy as a whole and its future evolution."<br>
                    <br>
                    <b>OER:</b> "If Oman is admitted, will there be a short-term phase of inconvenience as the Sultanate ensures its laws, regulations and industries are conforming to WTO agreements and rules? Has this been the case for other countries in the past? If so, what have been the most challenging aspects for them?"<br>
                    <br>
                    <b>MM:</b> "Many of the countries which have been admitted recently to the WTO, or are in various stages of accession, are former centrally-planned economies in transition to a market based system. These countries find that WTO accession runs hand-in-hand with economic reform. The nature of the accession negotiations also underscore to acceding governments the use of trade policy instruments in the context of overall national economic interests, which are often very different from those pursued by vested interest groups seeking sector-specific protection.<br>
                    <br>
                    In the case of Oman, most of the new laws and regulations imply no burden. They refer to areas previously unregulated in Oman, such as intellectual property, or areas where Oman's practices have been similar to WTO rules, but these practices were not consolidated and formalized (e.g. customs valuation procedures). Should you find these new rules cumbersome, you should remember that these are the rules under which business is conducted world-wide. In addition, I would like to point out that many of the WTO rules are, in fact, so-called "good governance" principles, i.e. publication of trade-related information, transparency, time limits for administrative decisions, right of appeal, etc. - measures which are designed to make life easier for business people.<br>
                    <br>
                    I honestly look forward to the day - very soon - when I shall be able to welcome Oman into the
                    WTO!&quot;</font></p>
