<b>
<font face="Verdana" size="4">Revving up on aviation<br>
</font></b><font face="Verdana" size="1"><b>Al Maha increased its market share by boosting retail and bulk sales</b><br>
<br>
<img border="0" src="cover_6b.jpg" width="175" height="218" align="right" hspace="6" vspace="4">Riding on the overall boom in the economy in general and the construction 
industry in particular, Al Maha Petroleum Products has improved its financial 
performance but had to be content with sixth rank this year. The company posted 
a revenue growth of 30 per cent in the financial year 2006 as compared to 22 per 
cent in 2005.<br>
<br>
In a short space of time span, Al Maha Petroleum Products Marketing Company 
S.A.O.G has many feathers in its success cap. Tremendous growth followed the 
opening of the first fuel station at Al Khuwair on 24th September 1994. In 1995 
two more stations were added. One located in the Capital area and the other in 
Sohar. By the end of 1999, 52 filling stations had sprung up at various places 
all over the country. Today the company &#8206;proudly boasts of having over 145 
filling stations till end of May 2007 throughout the country that have been 
catering to the fuel needs of even the remotest area in Oman. The right 
combination of experts, professionals, systems, technologies and affiliates 
together with an innovative head office has made this possible in such a short 
span of time.<br>
<br>
Besides being in the retail business, Al Maha has become prominent supplier of 
fuel and lubricants to a number of Government Ministries. It is also a key 
supplier to a number of national and international companies operating in the 
Sultanate of Oman.<br>
<br>
Taking advantage of the overall growth of the economy in 2006, we focused on 
enhancing our filling stations network to increase our market share in the 
retail sector and made substantial gains in bulk and aviation fuel sales.<br>
<br>
<b>The year that was<br>
</b>Even though the crude oil prices varied during the year, there was no 
significant variation in the purchase and selling price of the company’s 
products, except in the case of aviation fuel that increased during the later 
part of the year.<br>
<br>
The primary challenges identified in 2006 were shareholders’ wealth, net profit, 
competition, customer service, provision of better infrastructure facilities, 
tight profit margins and environmental issues. The net profit of RO4.1 million 
means a growth of 25 per cent over RO3.3 million in 2005. The growth in sales of 
30 per cent as compared to 2005, is attributed to the growth in sales volume by 
19 per cent in all sales sectors and the increase in selling prices of aviation 
fuel and diesel in May 2005. Earnings per share grew by 25 per cent, from baiza 
545 in 2005 to baiza 681 in 2006 due to the increase in net profit. Return on 
equity ratio increased from 22.7 per cent last year to 25.5 per cent in 2006. 
The Board of Directors recommended a dividend of baiza 400 per share (40 per 
cent) against 2006 profit (baiza 350 per share was paid against 2005 profit).<br>
<br>
Retail sales grew by 16 per cent in 2006 as compared the year before, and 
contributed to the favourable revenue performance. This remarkable increase was 
largely attributable to the increase in sales volume by 13 per cent. The 
increase in retail sales volume was the result of a number of factors including 
the construction of 19 filling stations, which brought the total number of 
filling stations to 138 at the end of 2006, and the normal growth in the retail 
sales sector. Al Maha smart fuel cards also continued to contribute to the 
increase. Retail sales are expected to grow further by 16 per cent in 2007.<br>
<br>
Bulk sales saw an increase of 30 per cent in 2006 as sales volume went up 
following increased construction activities all over Oman and in Sohar region in 
particular. Bulk sales are expected to grow by 12 per cent in 2007.<br>
<br>
Aviation commercial sales grew by almost 100 per cent in 2006 over last year 
mainly due to the increase in sales volume and selling prices in response to 
similar increase in purchase prices. On the other hand, the company–as in 
previous years–managed to achieve a Lost Time Injury (LTI) Free Year in 2006 as 
well. To maintain the trend, Al Maha continues to look upon all company and 
contractor employees to comply with laid-down procedures and safe working 
practices at all times. In this regard, filling station operations were also 
critically audited to ensure the company’s dealers and forecourt staff fully 
complied with its operating procedures.<br>
<br>
With Omanisation reaching 82 per cent at the end of 2006, the company is 
reviewing its strategies and policies to enable staff to progress and pursue a 
long-term career with it. <br>
<br>
The company has in place Health, Safety and Environment Management System Manual 
which was revised to suite the local operating conditions. During the year, the 
focus was on training the drivers in groups as well as individually as the 
drivers work without supervision most of the time. In order to achieve this 
objective, the firm recruited a young and energetic Omani HSE training 
executive. It has drawn HSE action plans at various levels and the 
implementation of these plans are being monitored by the respective HSE 
committees. They are fully supported by Corporate HSE Committee. <br>
<br>
It also has drawn up HSE related training programme for all our employees at all 
locations, and as a first step, completed ‘Defensive Driving Course’ for all its 
employees. The feedback looks very good. In the year 2006, filling station 
operations were critically audited to ensure that the company’s dealers and 
forecourt staff fully comply with the operating procedures.</font></p>
