<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Case for enhancing inter-Arab trade</font></b><font face="Verdana" size="1"><br>
<b>The 22 Arab nations should look at realising trade opportunities among 
themselves, writes Dr Jasim Husain Ali<br>
</b>
<br>
<img border="0" src="closeup.jpg" width="145" height="281" align="right" hspace="8" vspace="4">As expected, the 19th summit of the Arab League, held in the Saudi capital of 
Riyadh in late March, focussed mainly on political matters. The leaders spent a 
considerable amount of time discussing matters such as the violence in Iraq, the 
Iranian nuclear programme, the Palestinian question, divisions in Lebanon, 
problems facing Sudan and Somalia, to name a few.<br>
<br>
But, the Arab officials also managed to discuss the issue of economic 
cooperation amongst the 22-member bloc. In fact, an extraordinary outcome of the 
summit was the call for convening of a special summit dedicated to increasing 
economic cooperation amongst the member-states. Kuwait offered to host the 
proposed Arab Economic Summit, though no date has been fixed for the gathering.<br>
<br>
<b>Low trade levels<br>
</b>Increasing trade amongst Arab countries is vital, as inter-Arab trade is 
notoriously low. Both trade and investments amongst Arab states comprise less 
than 15 per cent of their total worldwide. The discrepancy is commonly 
attributed to the absence of reliable data. Unfortunately, the Arab League’s 
secretariat and likewise the Arab Monetary Fund are not noted for releasing 
statistics on inter-Arab trade on a consistent basis, which is vital for 
researchers to develop time-series studies.<br>
<br>
Anyway, most of this inter-Arab trade takes place within Gulf Cooperation 
Council (GCC). In turn, this reflects growing economic integration within the 
GCC. The six-nation grouping has in place a customs union dating back to the 
start of 2003. The agreement requires adoption of a common external trade policy 
with non-members. The GCC states are expected to complete the customs union 
requirements by the year-end and accordingly commence the common market status. 
This will allow for unrestricted access for factors of production to move around 
member-states.<br>
<br>
At the same time, most studies agree that the Arab League suffers from low 
inter-Arab investments. By one account, Arabs invest 14 per cent of their total 
investments in fellow Arab nations. There are many reasons for this, including 
available investment opportunities and laws. But the fact is that some 200 
countries compete for attracting foreign investments in order to address their 
economic opportunities and challenges.<br>
<br>
<b>GCC integration</b><br>
To be sure, GCC states have developed competitive advantages in certain sectors. 
For example, Saudi Arabia has an advantage in producing agricultural products 
thanks to governmental efforts to attract investments in farmlands. The Kingdom 
is blessed with different terrain, making certain parts fit for raising crops. 
Also, the authorities can afford to extend support to the farmers, not least 
because Saudi Arabia is the world’s largest exporter of crude oil.<br>
<br>
On its part, Oman has developed an edge in producing packaged foodstuffs thanks 
to developments of industrial areas across the Sultanate. Likewise, Qatar has 
the unique advantage of producing natural gas and gas related products. In fact, 
Qatar is on the verge of becoming the world’s largest exporter of liquefied 
natural gas (LNG) on the back of the on-going expansion of the gas sector. 
Currently, Indonesia is the largest exporter of LNG in the world.<br>
<br>
Also, Bahrain has placed itself as the leading regional base for sophisticated 
financial services notably Islamic banking products. Still, the UAE, notably 
Dubai, is credited for attracting more investors.<br>
<br>
Nevertheless, observers describe GCC economies as competing rather than 
complimentary ones, especially because all six nations export petroleum and 
petrochemical products.<br>
<br>
<b>Geographical factors<br>
</b>Several factors explain the low-level of trading amongst Arab countries. One is 
geographical. The 22-strong members of the Arab League are spread over two 
continents – Asia and Africa. Geography is vital in enhancing trade. The highest 
bilateral trade takes place between the neighbouring states of US and Canada 
(two-way trade amounts to $500 billion annually).<br>
<br>
In reality, distance is vast between some states, such as that between Morocco 
and Iraq. Direct flights take about eight hours non-stop. Not surprisingly, the 
air link amongst Arab countries is not necessarily frequent or direct. For 
example, there is no direct flight between Bahrain and Algeria. Thus, Bahraini 
visitors fly to Algiers via Paris.<br>
<br>
In fact, GCC airlines such as Emirates, Etihad, Saudi Arabian Airlines and Qatar 
Airways are now helping strengthening air links amongst Arab nations. Also, 
no-frills carriers such as Air Arabia are helping in the process of uniting Arab 
cities.<br>
<br>
In contrast, the frequency of daily flights between India and the GCC is 
exceptional, more than 20 flights per day. Partly, this relates to geographical 
proximity. Also, air link is vital to transport thousands of Indian nationals 
working in the GCC states. Still, there are untold business opportunities 
between the two sides. Suffice to say that in a span of 10 days in March, top 
leaders from the UAE and Bahrain, namely Shaikh Mohammed Bin Rashed Al Maktoum 
(Vice President and Prime Minister of the UAE and Ruler of Dubai) and Shaikh 
Salam Bin Hamad Al Khalifa (Crown Prince of Bahrain) paid working visits to 
India.<br>
<br>
<b>Globalisation trends</b><br>
The trend towards globalisation is exemplified by the desire of Arab countries 
to sign free trade agreements (FTA) with the US. At the moment, the US has 
separate FTAs with Jordan, Morocco, Bahrain and Oman. In reality, individual 
Arab countries cannot be blamed for taking unilateral decisions to secure 
accords with the US. These countries desire to get access to the vast American 
economy (the largest in the world) to help solving their outstanding problems, 
such as unemployment.<br>
<br>
These Arab countries have sought accords with the US to obtain unrestricted 
access to the substantial American economy. The US gross domestic product (GDP) 
stands at $12 trillion. In turn, Japan’s GDP, the second largest in the world, 
is just half of that of the US. More importantly, the US import bill amounts to 
nearly $2 trillion, by far the highest in the world. Thus, by signing FTAs with 
the US, Arab countries of Jordan, Morocco, Bahrain and Oman intend to solve 
their own economic problems.<br>
<br>
Between some Arab countries, trade is next to negligible. For example, only 
meagre bilateral trade occurs between Bahrain and Mauritania. Also, some Arab 
countries are going through extraordinary times. Somalia, for example, is 
experiencing an abnormal situation with looming civil war. Again, violence in 
Iraq suggests that the country is heading for the unknown. The primary focus in 
both Somalia and Iraq is not trade but rather security.<br>
<br>
<b>Way forward</b><br>
Certainly, the European Union (which recently celebrated the 50th anniversary 
for signing the Treaty of Rome) stands as the best model for Arab countries. The 
EU encompasses 27 member-states. Trade amongst EU countries covers about half of 
the bloc’s worldwide trade. A key advantage for the EU lies in the fact that 
numerous countries have their own unique competitive advantages. For example, 
Germany is noted for manufacturing durable cars and equipment. Similarly, thanks 
to supporting weather, Spain is noted for producing desirable agriculture 
products.<br>
<br>
The solution for improving trade within Arab countries may lie in member-states 
specialising in different areas. However, each country faces its own unique 
challenges and opportunities and explains why some Arab countries opted to 
clinch accords with the US covering trade and investments. In return, the US 
stipulated that these countries must streamline their business and labour laws 
in advance of implementing the FTAs. For example, the US endorsed the FTA with 
Bahrain in January 2006 but implementation was delayed until August. The US 
demanded that Bahrain affects legislation dealing with protecting intellectual 
property rights (IPRs) in order for the FTA to go into effect. The legislation 
includes penalties against violators. Bahrain submitted to the request hoping to 
entice American investments. Recently, Kraft of the US decided to set up a plant 
for producing dairy products in Bahrain.<br>
<br>
In reality, Arab countries are not dependent on each other for trading purposes. 
Still, such adverse situation should provide the needed impetus to seek ways to 
improve inter-Arab trade and investments. In fact, the surge in oil revenue 
meant that the revenues of the six-nation GCC increased from $100 billion in 
2002 to $325 billion in Much is clearly at the stake for the 22-nation Arab 
League when it comes to dealing with each other. </font>
