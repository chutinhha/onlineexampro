        </font></b><font face="Verdana" size="1">The Directorate General of Export Development (DGED) of The Omani Centre 
        for Investment Promotion and Export Development (OCIPED) whose role is 
        to develop non-oil exports of Omani origin is happy to announce the 
        significant growth achieved by the Sultanate of Oman in its Omani origin 
        exports during 2006. The value of Omani origin exports have touched 
        RO812.3 million during 2006 as against RO555.3 million in 2005 
        registering an impressive growth rate of 46.3 per cent. During the 6th 
        Five Year Plan, the non-oil Omani origin exports (excluding LNG) grew 
        from RO265.8 million in 2001 to RO555.3 million in 2005 at a compounded 
        annual growth rate of 20.2 per cent. The export performance has been in 
        line with the export strategy adopted by OCIPED, which has identified 
        thrust products and target markets for the short term (2001–2005), 
        medium term (2006–2010) and long term (2011–2020).<br>
