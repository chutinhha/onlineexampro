<font face="Verdana" size="2" color="#CC3300"><b><br>
</b></font><font face="Verdana" size="1">
                        <img border="1" src="Dossier_big.jpg" width="180" height="239" style="border: 1px solid #3350A6; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" align="left" hspace="5" vspace="5"><b>Oman Economic Review </b>(OER), the premier business magazine in Oman, after 
changing the business magazine landscape has launched OER DOSSIER - an exclusive 
business-to-business publication focusing on key sectors every month.<br>
<br>
The inaugural issue of OER Dossier was launched at the premier industry event 
'Interiors &amp; Buildex 2007' on 15 May and has the latest updates on the 
Construction Industry. <br>
<br>
It encompasses the global and regional scenario, overview of the construction 
business in Oman and the underlying factors and implications for the economy 
peek into the major construction projects, views of the industry experts, 
interviews with the industry captains and the shape of things to come in the 
future. In nutshell, it provides a comprehensive outlook on the construction 
sector in Oman.<br>
<br>
“Every month, OER DOSSIER will provide in-depth information on the respective 
domain to keep the readers abreast of the latest developments and trends in 
their industry sector,” said Alpana Roy, Assistant General Manager- United Press 
&amp; Publishing LLC. <br>
<br>
Exclusive interviews with the honchos, views of the connoisseurs, relevant 
market statistics, corporate profiles, opportunities for new business and 
important industry news will be the hallmark of OER DOSSIER. <br>
<br>
Roy added, “Whether one is looking for new suppliers for procurement or buyers 
for the products/services or experts’ guidance, OER DOSSIER will be the perfect 
information partner to keep the readers ahead of the competition!”<br>
