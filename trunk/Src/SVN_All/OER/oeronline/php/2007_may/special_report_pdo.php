  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font size="1" face="Verdana">‘Reality check’ for internal perceptions and plans versus external realities, 
offering a filter for an organisation’s strategic initiatives</font></li>
</ul>
<p align="justify" style="margin: 3px 10px"><b><font size="1" face="Verdana">Internally:</font></b></p>
<ul>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font size="1" face="Verdana">Enterprise-wide (boundary-spanning) management and synergistic, efficient 
resource allocation</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font size="1" face="Verdana">Timely and contextually-relevant communication or organisational goals vis-à-vis 
issue impact thereby improving capacity for individuals to ‘speak for’ the 
organisation </font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
