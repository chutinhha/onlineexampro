</font><font face="Verdana" size="2" color="#CC3333"><b>Intel says new chips up to 40 pc faster</b></font><font face="Verdana" size="1"><br>
<img border="0" src="scan.jpg" width="100" height="68" align="left" hspace="6" vspace="4">US Chip giant Intel’s upcoming Penryn processors will boost computer speed by 40 
per cent for gaming, compared to existing top-line chips, the company said. The 
chips, expected to hit the market in the second half of 2007, would also 
increase performance by an estimated 45 per cent for bandwidth-intensive 
applications, Patrick Gelsinger, the company’s general manager for digital 
enterprise, said at the Intel Developer’s Forum in Beijing. Gelsinger added the 
chip would allow performance increases of about 15 per cent for imaging-related 
applications, 25 per cent for 3-D rendering, and more than 40 per cent for video 
encoding.<br>
<br>
Intel, the world’s largest semiconductor maker, held its developer’s forum in 
China just a month after announcing plans for a US$2.5 billion chip plant in the 
north-eastern Chinese city of Dalian. The new plant reflects a new focus by the 
company on China which is expected to help the country evolve into a more 
high-tech manufacturing centre.<br>
