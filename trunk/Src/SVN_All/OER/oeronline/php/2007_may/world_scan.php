<font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3333"><b>World economy in robust health, says IMF</b></font><font face="Verdana" size="1"><br>
The world economy is poised for its longest expansion since the early 1970s 
despite a US housing slowdown and financial market instability, the 
International Monetary Fund has said.<br>
<br>
In its latest World Economic Outlook report, the IMF said growth in global gross 
domestic product (GDP) would reach 4.9 per cent both this year and next. While 
down from 5.4 per cent in 2006, it would take the current expansion into its 
fifth and sixth years, the global economy’s best performance since before the 
1970s’ oil shocks. “By and large things look very good,” IMF chief economist 
Simon Johnson told a news conference, playing down what is likely to be 
America’s weakest economic performance in five years.<br>
<br>
Unlike in the 1970s, according to Johnson, growth is far more widespread as 
China and India extend dramatic transformations, while Europe and Japan look set 
to outstrip US growth this year. “Everyone around the world is sharing in this 
prosperity. I hesitate to say that it is unprecedented, but it is very 
impressive,” he said. “While the US indeed may have sneezed, it appears to be a 
mild sneeze so far, and not likely to spread.” Given the right policy mix 
worldwide, Johnson added, “I think we can sustain global growth around 5 per 
cent a year for some time to come.”<br>
<br>
Eurozone growth is on course to overtake the United States for the first time 
since 2001, although both regions are expected to lose steam this year, the Fund 
said in its twice-yearly report. The IMF fretted about the potential for a 
sharper slowdown in the US, whose GDP growth was forecast to dip to 2.2 per cent 
in 2007 from 3.3 per cent last year. That was a big downgrade from the 2007 
growth pace of 2.9 per cent that the IMF had predicted in September. There are 
“some very tentative signs” that the US housing market could be stabilising, but 
the Fund warned that the property shakeout has some way to run.<br>
