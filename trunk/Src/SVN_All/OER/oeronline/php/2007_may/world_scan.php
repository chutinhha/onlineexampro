<font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3333"><b>Nestle confirms Gerber deal</b></font><font face="Verdana" size="1"><br>
Nestle SA, the world’s largest food company, has confirmed a US$5.5 billion cash 
deal to buy US baby-food maker Gerber from Novartis AG, furthering its push into 
the nutrition business. Nestle said the deal would give its growing nutrition 
division – which includes infant formula, baby food, medical nutrition, weight 
management and performance nutrition – annual sales of around 10 billion Swiss 
francs (US$8.21 billion). Gerber, a household name in the United States, will 
generate an estimated US$1.95 billion in net proceeds from sales in 2007, Nestle 
said.<br>
<br>
Although Nestle is the world’s biggest maker of infant nutritional products, it 
lacked a baby food brand in the US and has been keen to buy Gerber for more than 
a decade. An acquisition of Gerber would continue Nestle’s push into health and 
nutrition. Nestle recently bought the medical nutrition business of Novartis for 
US$2.5 billion, but that deal did not include Gerber.<br>
