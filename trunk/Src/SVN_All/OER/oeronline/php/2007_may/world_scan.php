</font><font face="Verdana" size="2" color="#CC3333"><b>Mangoes, bikes to lift Indo-US trade<br>
</b></font><font face="Verdana" size="1">India and the United States expect a new high on bilateral trade, revved up by 
imports of US motorcycles and the sale of Indian mangoes to Americans. After 
years of blocking import of Indian fruits, the United States last year allowed 
Indian mangoes into its domestic markets. This followed a visit to New Delhi by 
US President George W Bush in March 2006.<br>
<br>
“Last year, total bilateral trade in goods rose to nearly US$32 billion,” US 
Trade Representative Susan Schwab told a business forum in New Delhi. “This 
year, total combined trade in goods and services could well surpass US$50 
billion,” she said. Schwab’s Indian counterpart Kamal Nath said Indian and US 
trade delegations had exhaustive discussions on bilateral trade “from Harley 
Davidsons to mangoes.” He said, “The good news is that Harley Davidsons will 
come to India and Indian mangoes will go to the United States”.<br>
<br>
Both sides also discussed infringement of intellectual property rights with New 
Delhi asking Washington to crack down on those marketing pirated versions of 
Indian films in the US market. “We raised the issue of widespread availability 
of pirated Indian films and music in the US... it can be found in any grocery 
store. The US has assured us it will look into it,” Nath said.<br>
