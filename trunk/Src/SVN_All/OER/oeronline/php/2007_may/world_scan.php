</font><font face="Verdana" size="2" color="#CC3333"><b>EU moving towards cross-border roaming caps<br>
</b></font><font face="Verdana" size="1">Calling home from European beaches will be cheaper for mobile phone users this 
summer if EU lawmakers and the European Commission get their way on planned caps 
for so-called roaming rates. Plans to force operators to charge less on 
cross-border calls in the European Union got a boost last week when a key EU 
panel backed the package in the face of fierce lobbying from operators fearful 
of losing revenues.<br>
<br>
The European Parliament’s industry committee voted broadly in favour of capping 
the price of calls made abroad in the EU at 40 cents per minute and 15 cents for 
calls received abroad. The committee’s backing sets the stage for a vote before 
the full parliament in a plenary session on May 10 in Brussels and a final 
decision by EU telecom ministers on June 7 in Luxembourg. After the vote, the 
bill’s rapporteur, Austrian conservative Paul Ruedig, said: “We are sending a 
very clear message to the consumers and telecom operators. It’s a good basis for 
our deliberations. I now expect swift and successful negotiations with (EU 
governments) so that the new roaming regulation can be enforced indeed before 
summer,” he said.<br>
