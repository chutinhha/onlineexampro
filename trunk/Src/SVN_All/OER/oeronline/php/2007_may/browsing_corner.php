		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><b><font face="Verdana" size="4">Stumbling on 
        happiness</font><font face="Verdana" size="1"><br>
        <br>
        <img border="0" src="book.jpg" width="110" height="157" align="left" hspace="6" vspace="4">Harvard Professor Daniel Gilbert describes how well the human brain can 
        imagine its own future, and how it can predict which of those futures it 
        will most enjoy<br>
        <br>
        </font></b><font face="Verdana" size="1">We human beings seem to be on a perpetual quest for happiness in our 
        lives. We tend to live in the future. This is attributed to the frontal 
        lobe – the last part of the human brain to evolve, slowest to mature and 
        first to deteriorate in old age. It is a machine which helps us, unlike 
        the other animals, to vacate the present and experience the future 
        before it happens, says renowned Harvard psychologist Daniel Gilbert.
        <br>
        <br>
        What exactly is happiness? The latest scientific research in psychology, 
        cognitive neuroscience, philosophy and behavioural economics, reveals 
        what scientists have discovered about the uniquely human ability to 
        imagine the future, and about our capacity to predict how much we will 
        like it and thus our happiness when we get there. Among various 
        real-life situation observations in the book, there’s one about a 
        fascinating contrast between two revolutionary men – the first was 
        hanged and the other committed suicide. The first man was happy on the 
        last day of a wretched existence and the other unhappy on the last day 
        of a fulfilling life! <br>
        <br>
        Gilbert feels had we been in their shoes we would have experienced 
        precisely opposite emotions. So what was wrong with these guys? Nothing, 
        concludes the Harvard Professor, the problem lies with us: we make 
        decisions about our lives based on how it would feel if this event 
        happened and that one didn’t. Research suggests that when people make 
        predictions about their reactions to future events, they tend to neglect 
        the fact that their brains have performed the filling in tricks as an 
        integral part of the act of imagination. People misremember their past 
        and there is a tendency to fill in the holes. <br>
        <br>
        “Most of us have a tough time imagining a tomorrow that is terribly 
        different from today and we find it particularly difficult to imagine 
        that we will think, want or feel differently then we do now.” 
        Imagination has its flaws – it’s either too liberal or conservative, a 
        major shortcoming in the prediction game that we all seem to do to 
        secure our tomorrow.<br>
        <br>
        Gilbert also decisively deals with life’s bitter truths: “Wonderful 
        things are especially wonderful the first time they happen but their 
        wonderfulness wanes with repetition. We soon begin to adapt to it and 
        experience yields less pleasure each time.” Psychologists call it 
        habituation and economists diminishing marginal utility! Hence stumbling 
        on happiness is not guaranteed by changing houses or spouses. So also 
        with vitamins pills and plastic surgery. <br>
        <br>
        Gilbert goes on to recount cases where after major tragedies people have 
        bounced back and coped marvellously. In fact, a significant portion of 
        these people claim their lives have been enhanced by the experience. On 
        the other hand, getting where you wanted to be doesn’t yield the 
        happiness you thought it would bring. The book is not an instruction 
        manual which the reader can follow; instead this is a book that 
        describes what science has to tell us about how well the human brain can 
        imagine its own future, and how and how well it can predict which of 
        those futures it will most enjoy. <br>
        <br>
        A well-researched book with copious notes, figures and illustrations; 
        best to stumble on it rather than go looking for it lest it disappoints 
        you! <b><br>
        <br>
        – Uma Devi Jadhav</b></font></p>
