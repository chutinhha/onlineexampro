<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4"><b>Get the best out of your AC</b></font><font face="Verdana" size="1"><br>
<b>Is buying an AC or refrigerator on your mind, but you’re fretting about its 
maintenance? Follow these tips and save some on your electricity bills!<br>
<br></b>The maintenance of an AC is an area that most consumers and commercial clients 
need to learn about. Most consumers are averse to maintaining their ACs and 
customarily call up dealers for service when a little bit of care at their end 
can help them in the long run. Simple things like regular cleaning of the 
filters, etc., helps increase the life of one’s AC and keeps the dealer away.<br>
<br>Alternatively, it is more prudent for one to enter into an annual maintenance 
contract with the authorised service centre of your brand of AC, so that the 
units are maintained at a nominal cost with little hassle. Statistics prove that 
this cost is negligible when compared with the cost of replacement as also the 
unavoidable inconvenience resulting from a breakdown. The following few tips 
will help one maintain an air-conditioner in an easy simple way:</font><ul>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Understand that an air conditioner, apart from cooling, also conditions the air 
inside your room like humidity, dust, order, fungus, bacteria, etc. </font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">All good quality ACs have cooling, humidity control, dust removal, anti fungal 
and anti-bacterial functions and these need to be used properly.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Split ACs are now affordable, very quiet and energy saving as compared to window 
ACs. They are also more hygienic and prevent birds from making their nests in 
air gaps, rodents creeping in and air from leaking.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Clean the filter of your AC once a fortnight, or when the indicator lamp on the 
front panel lights up.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Keep the doors of your rooms open for a few minutes every day to allow some 
fresh air to enter.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Do not spray any type of deodorant or perfume into your split ACs. Doing so will 
result in foul smell, necessitating a cleaning of the indoor unit.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Avoid smoking in an AC room as indirect smoke can harm other family members and 
it is very difficult to remove the odour even by servicing.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">If you smell bad odour, it normally will not be from your AC but from your 
carpet, curtains, linen, excessive sweat, etc. However, if the filter is not 
cleaned routinely or the drainage is clogged and water remains in the drain pan 
for long, it can cause foul smell. Here, it is essential that the drainpipe be 
connected properly to prevent foul smell from entering through the pipe.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Keep the exhaust fan of your bathrooms ON when you are at home.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Keep the split AC ON even when you are out of home at a temperature of 27 degree 
C. This will keep your home cool and healthy, especially when the outside 
temperature is above 42 degree C, with high humidity. A good quality AC is meant 
to be ON 24 hours a day.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">In Oman today, an AC is needed for not more than seven months a year. Using the 
AC when indoor temperature is less than 25 degree C is waste of energy. Between 
November and March, do not switch ON the AC in fan mode, instead use a ceiling 
fan that will give you comfortable and natural sleep.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">When the outside temperature goes above 42 degree C, keep the bedroom and living 
room ACs ON at a temperature setting of 27 degree C even if you are out of the 
room. This will prevent the room walls and roof from becoming excessively hot. 
Hot walls and roof lead to continuous running of the AC’s compressor, resulting 
in excessive power consumption and inefficient cooling. When you re-enter the 
room, you can adjust the temperature settings. Your electricity bills will not 
be high as the compressor will shut down on reaching the set temperature, 
leaving you with pleasant feeling once you enter your room. In such times, the 
AC can be turned off after 10 PM as the room temperature will remain at around 
25 degree C and a ceiling fan running at slow speed will give you a comfortable 
sleep.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">If you own a villa, plant trees around the building, as these can help you save 
on your electricity bills.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Insulate your rooftop professionally and use 
  good quality insulated bricks for the walls. Use thick curtains so that the 
  heat rays do not enter the house / flat.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Keep the outdoor unit of your split air conditioner in a shaded area or place it 
in such a way that afternoon sunlight does not hit it directly. </font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Get the AC checked and serviced by your distributor once a year for any gas leak 
and get the outdoor unit washed using high pressure water. This will help you 
save on your electricity bills.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Cleaning the filter every fortnight is very important to save energy. Users 
themselves can do filter cleaning and use service centres for other routine 
services;</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Try to select energy-efficient ACs.</font></li>
  <li>
  <p align="justify" style="margin-left:-10px; margin-right:10px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Electricity bill of a three-bedroom villa or apartment with about five occupants 
under above conditions will not exceed RO 45 per month in peak summer, even as 
one stays fresh and comfortable all day long.</font></li>
</ul>
