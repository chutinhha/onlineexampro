<b>GCC Oil Sector<br>
</b>The world oil demand in 2007 is expected to grow by 1.5% or 1.2mb/d to reach 
85.37mb/d. Majority of the world’s incremental oil demand is expected for use in 
the transportation sector. Higher demand is expected from Asia, specifically 
China, India and the Middle East. Asia’s strong oil consumption has shifted the 
pattern from OECD countries which are no longer the growth driving factors. The 
economic growth in the developing countries is the main element behind its high 
oil consumption. On a regional basis, China and the Middle East are expected to 
contribute the most to the rise in demand with an estimated growth of 0.6mb/d 
and 0.35mb/d, respectively.<br>
