					event for the second year running and excited that this year 
					it will be taking place on the first green golf course in 
					Oman. This is particularly apt as Nawras is no stranger to 
					introducing many communication ‘firsts’ for customers. Our 
					long term commitment will see Nawras Business Solutions 
					presenting OER CEO Golf with pleasingly different networking 
					opportunities in this informal environment for the next 
					three years.” About the day’s programme, Cormack said, “Last 
					year’s event was described as a tremendous success and 
					without being able to give too much away at this stage, I 
					can certainly say that we are looking forward to raising the 
					bar even further this year. Each OER CEO Golf day is getting 
					bigger and better and Nawras Business Solutions will be 
					helping to ensure that the 2010 event will be a talking 
					point for a long time to come.”<br>
					<br>
					Oman’s customer-friendly mobile communications service 
					provider, Nawras, is committed to enhancing people’s daily 
					lives, making life more versatile and always a pleasingly 
					different experience. For real corporate mobility, and 
					enhancing business efficiency, Nawras Business Solutions has 
					been the right choice for many organisations. With their 
					vision to enrich the lives of people in Oman, Nawras 
					constantly strives to be caring, excellent and pleasing, 
					striving to build close relationships. They believe that 
