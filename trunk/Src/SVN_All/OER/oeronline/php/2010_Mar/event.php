					avenues that significantly enhance the quality of people’s 
					lives. Considering their corporate ethos, it is not 
					difficult to understand why they have agreed to be the 
					Corporate Presenters of the 6th Edition of OER CEO Golf too.<br>
					<br>
					Speaking about the three-year agreement, Sandeep Sehgal, CEO 
					of United Media Services says, “The partnership reiterates 
					Nawras’ commitment to the event and brings value to it. Over 
					the years OER CEO Golf has grown into a media asset that 
					gives partners an opportunity to interact and engage with an 
					elite target audience. The inherent strength of this 
					property along with a preferred partner like Nawras ensures 
					that OER CEO Golf will grow from strength-to-strength in the 
					coming years.”<br>
					<br>
					<b>On board<br>
					</b>In addition to Nawras, OER CEO Golf is being partnered 
					by National Bank of Oman (NBO). David Power, NBO’s general 
					manager, chief of Retail Banking said: “NBO is well known in 
					the banking industry for launching many firsts. We are 
					indeed honoured to sponsor the prestigious OER CEO Golf 
					Tournament, which will be held at Muscat Hills, Oman’s first 
					green golf course. The decision insures that professional 
					golf will continue to grow in the Sultanate as we look 
					forward to a long lasting relationship with OER and their 
					many initiatives.” Humayun Kabir, NBO’s general manager, 
					chief of Business Banking said: “NBO continues to remain 
					committed to supporting all major sporting and community 
					related events in the Sultanate. We are proud to be 
					‘Associate Presenter’ of the prestigious ‘OER CEO Golf 
					Tournament’. The tournament provides us an ideal opportunity 
					to mingle informally with our valued customers<br>
					and high profile members of Oman’s business community.”<br>
					<br>
					Rolex from Khimji’s Watches continues to partner the event 
					this year too. Says Anil Khimji, director, Khimji Ramdas, 
					“Rolex is associated with premium golf events globally; and 
					it was quite natural for Khimji’s Watches to have tied up 
					with OER CEO GOLF from its very inception. Rolex stands for 
					perfection and class and these values resonate well with all 
					those who would be participating in this event. Khimji’s 
					Watches, the final word in luxury watches and accessories in 
					Oman today, is happy to be associated again with this much 
					anticipated and high profile event of the year. It’s a win 
					win situation for us as event partners; but for the most 
					promising golfer-to-be this could be the most exciting win 
					of all; when he takes home a Rolex. I wish this programme 
					grand success.”<br>
					<br>
					The other partners of the event are Times of Oman and Al 
					Shabiba, the publicity partners; Hi!, Faces and Al Youm Al 
					Sabe, the media sponsors. The category partners include 
					Cybex (Fitness), OUA (Beverage), Infoline (Call centre), Axa 
					Insurance (Insurance), Al Sawadi Beach Resort (Hospitality), 
					Oman Printers &amp; Stationers Co (Printing), Signature (Luxury 
					partner) and Muscat Hills (Venue). <br>
					<br>
					This year too, the day-long event is designed to provide 
					nail-biting action for CEO golfers. The main competition is 
					an 18-hole affair on a golf course that has been delicately 
					crafted around the natural Omani mountain ranges to produce 
					the first course of its kind in the Middle East. Players in 
					this competition will be able to experience, for the first 
					time in Muscat, what it feels to compete on a course 
					designed by internationally renowned golf course designer 
					Paul Thomas of Dave Thomas Associates, and constructed by 
					Southern Golf UK.<br>
					<br>
					The word has spread around, and the corporate world is 
					excited and abuzz about the 6th edition of OER CEO Golf. 
					“OER CEO GOLF has always been an exclusive event. It’s 
					Oman’s biggest and unique golfing-cum-business networking 
					extravaganza,” says Sehgal. “For us, it’s exciting that 
					Oman’s most awaited corporate golfing championship will see 
					the ‘who’s-who-of-Oman’ out there together on the greens.” 
					Green is not the only unique facet about this year’s OER CEO 
					Golf. Apart from the 18-hole main event, Water Golf is on 
					the cards. It’s an exciting event for golfers, golfers-to-be 
					and their spouses. At the Serenity Lake at Muscat Hills, 
					players will have to study the positions of a floating 
					green, and aim to get their golf ball onto it.<br>
					<br>
					New, exciting and fun-filled activities have been planned to 
					fill the day’s calendar, ending with the grand finale at the 
					awards dinner - an evening that’ll be loaded with 
					enthralling performances and mesmerising music. “This is the 
					opportunity for real top-flight brands to be associated with 
					a high-profile event in Oman. It’s become the most 
					anticipated up-market event of the year in Oman, and every 
					year, we try to make it bigger and more exciting. Throughout 
					the day, and well into the night, it is going to be the best 
					anyone has seen here in a long time,” says Alpana Roy, 
					executive vice president of UMS.<br>
