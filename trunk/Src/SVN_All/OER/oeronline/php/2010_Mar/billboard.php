					Oman co-sponsored the Biosphere Expedition which concluded 
					its 2010 survey of the Arabian Leopard among the remote 
					mountains of Dhofar region. To Shell Oman Marketing Company 
					this support comes in line with its sustainable development 
					plans and compliments its commitment to developing 
					ecotourism in the Sultanate of Oman. This expedition is 
					monitored and facilitated by the Office of Conservation of 
					Environment in the Diwan of Royal Court. The expedition is 
					especially significant since the Arabian leopard is the last 
					surviving species of Big Cat on the Arabian Peninsula and is 
					critically endangered. The team managed to achieve its 
					objectives for this year led by Ronald Seipold and scientist 
					Tessa McGregor. The Biosphere expeditions bring together 
					people from different walks of life to support wildlife 
					research and conservation in various parts of the world. 
					Like last year, the Biosphere Expedition this year worked in 
					the Dhofar mountains to look for signs of Arabian leopard 
					and to estimate the abundance of the important leopard prey 
					species in the area. The research team also conducted 
					interviews of local people to better understand the 
					relationship between local communities and the leopard. </font></p><hr width="90%" size="1">
