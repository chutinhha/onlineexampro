				Business has lots to learn and multiple avenues to learn from. 
				Management education is becoming better and more accessible. 
				There are more management books and journals today than ever 
				before. Then there are business magazines like OER that not only 
				inform but also attempt to guide and educate. With so much of 
				wisdom on offer, it is an irony that most successful people 
				don’t quote a publication or a course as the source of the most 
				profound advice that they got.<br>
&nbsp;<br>
				The advice they cherish mostly is usually the one that they got 
				from their parents, mentors or bosses. This probably has 
				something to do with the advice as well as the adviser. There is 
				something heartwarming and enduring about a definitive advice 
