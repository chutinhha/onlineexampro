            <img border="0" src="im34.jpg" align="left" width="250" height="141">What 
            is your leadership style? </b><br>
            My style could be described as “measured involvement with active 
            participation to generate high performances against assessed risk”.
            <br>
            Sustained effectiveness in leadership for business involves regular 
            team briefs, sharing and consideration of ideas and also of results, 
            respect for individuals, openness and learning that enable 
            priorities to be set that ‘fit’ within a strategic plan to optimise 
            the employment of and return on the economic resources.<br>
            <br>
            <b>What factors have helped shape your leadership style?</b><br>
            There have been several influences that have mixed to shape my 
            particular leadership style:<br>
            • A competitive instinct developed through sports. <br>
            • A thorough education expanded through professional training and 
            skills development.<br>
            • A broad-based range of international industry experience.<br>
            • Working in highly competitive business sectors. <br>
            • My own value system that focuses on supporting each and every 
            member of the team. <br>
            <br>
            <b>Can you provide an example of a leadership challenge you faced 
            and how you solved it?</b><br>
            I was assigned to lead a business team in Kuwait post the first Gulf 
            War. They had re-assembled as a team in the same company after the 
            conflict, but the business was now different and more closely 
            identifiable as a start-up operation than the one they had worked in 
            pre-conflict. Still the same group of people, but they were 
            struggling to work together effectively as a team. The approach that 
            we adopted to get a rapid improvement was:<br>
            • To make time available for the team to get out of normal routines 
            and enable them to identify the problems. <br>
            • To prioritise as a team which issues needed to be addressed first.
            <br>
            • To put in place our agreed plan on how to achieve our goals.<br>
            • To monitor the results.<br>
            • To give feedback to the team on successes as well as areas that 
            were still not right.<br>
            • To introduce some ‘fun’ into the workplace to help move away from 
            some of the earlier tensions. <br>
            <br>
            <b>What are you doing today to develop leadership within your 
            organisation?</b><br>
            The leadership process within our organisation has a formal 
            structure and also a more informal but nonetheless systematic 
            approach. The formal structure involves formulating a vision of the 
            future, planning a strategic approach and setting milestones to 
            measure progress. The informal systematic approach allows for 
            one-to-one conversations and listening to different points of view.
            <br>
            <br>
            <b>What is your communication strategy?</b><br>
            To be clear, systematic and to share the most appropriate 
            information to enable the team to make decisions. <br>
            <br>
            <b>How have you evolved as a leader in the last five years?</b><br>
            The ability to keep focused and to rely on your team to participate 
            with a similar enthusiasm, and for each of them to share the 
            responsibilities has created the greatest motivation to growth. <br>
            <br>
