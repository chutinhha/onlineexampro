            people have provided facilities at the turtle beaches , renovated 
            the falaj systems at Tiwi, provided accommodation at the Oryx 
            sanctuary and even counted leopards in Dhofar! <br>
            <br>
            <b>What strengths do you rely on most in your daily life as a 
            leader?</b><br>
            To be available to the people who are a part of the key relationship 
            structure that forms our team as well as to have time to invest to 
            help them achieve more. <br>
            <br>
