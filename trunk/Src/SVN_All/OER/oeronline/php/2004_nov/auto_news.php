<img border="0" src="auto1.jpg" width="150" height="80" align="right" hspace="6" vspace="4">Lexus LS 460 notched the top spot at the New York International Auto 
        Show recently by winning the ‘The ‘World Car of the Year 2007’ award. 
        The Lexus LS 460 had already bagged the prestigious ‘2007 Japan Car of 
        the Year’ early on. LS460 won this award after a meticulous selection 
        process from an entry list of 28 formidable contenders from all 
        Japanese, European and US luxury makes. Lexus LS 460 shone because the 
        WCOTY jurors considered the car to be “the embodiment of the Lexus 
        brand’s motto – the pursuit of perfection” and “classically luxurious, 
        with modern execution, unquestioned quality and more than ample power”. 
        The jurors also praised the Lexus for including a novel automatic 
        parking system and an 8-speed auto transmission, which helped it pip 
        nearest rivals to claim first place.<br>
