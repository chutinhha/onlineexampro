        </font><b><font face="Verdana" size="2" color="#CC3300">Racing in the snow</font></b><font face="Verdana" size="1"><br>
        <img border="0" src="auto5.jpg" width="125" height="83" align="right" hspace="6" vspace="4">Bridgestone Motorsport has produced a special Limited Edition F1 Potenza 
        Snow Tyre at the request of the BMW Sauber F1 Team, which will be 
        conducting a demonstration run of their F1.06 car at the prestigious 
        White Turf horse racing event in St.Moritz, Switzerland.Due to the 
        extreme conditions in which the BMW Sauber F1.06 will run, the special 
        F1 snow tyres, produced by Bridgestone Motorsport’s Technical Centre in 
        Tokyo, feature 420 studs on each of the front tyres and 588 studs on 
        each of the rear tyres. It took two Bridgestone technicians over 16 
        hours to manually insert all 2,016 studs into the double layer of tread 
        compound on one set of tyres alone.<br>
