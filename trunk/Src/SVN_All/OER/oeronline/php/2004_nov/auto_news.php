        </font><b><font face="Verdana" size="2" color="#CC3300">The luxury vans</font></b><font face="Verdana" size="1"><br>
        <img border="0" src="auto6.jpg" align="left" hspace="6" vspace="4">Mercedes-Benz brings to Oman a range of vans for private and commercial 
        applications. The award-winning Sprinter is offered in various 
        tailor-made versions to best cope with Middle East conditions, offering 
        a payload of up to 2,415 kg and loading volume of up to 13.4 m3. Two 
        such versions are the crew bus variant, which comes with 16 comfortable 
        seats or the panel van, which offers the largest possible cargo area in 
        its class and significant payload advantages. Mercedes-Benz vans marked 
        another major milestone with the very competitive Viano and Vito vans to 
        meet various private and business applications. The Viano has a capacity 
        of up to eight seats whereas is also available with nine seats in the 
        Vito. In addition to the ultra-safe design, both Vito and Viano are the 
        only vans in their class to be fitted as standard with Electronic 
        Stability Program (ESP), anti-lock braking system (ABS), acceleration 
        skid control (ASR), electronic brake force distribution (EBV) and a 
        brake assist system (BAS) to offer the maximum safety features for the 
        driver and any passengers.<br>
