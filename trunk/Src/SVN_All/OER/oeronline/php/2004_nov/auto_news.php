        The all-new Chevrolet Captiva has garnered fans from all quarters in 
        Oman. Offering one of the best value-for-money vehicles in the fast 
        growing compact SUV segment, the Captiva has fine ride and handling 
        qualities offering customers a great driving experience. The five-door 
        Captiva is the first compact SUV from Chevrolet not only for the Middle 
        East but also the global market place. Captiva combines all the 
        ruggedness of an SUV with the stylish lines and characteristics of a 
        mid-size sedan. With its compact dimensions, strong, sporty design, and 
        class leading range of features even in the base model version, the 
        Captiva has the power to please with a choice of two engines – 2.4 L and 
        3.2 L – delivering 136hp and 230hp respectively.<br>
