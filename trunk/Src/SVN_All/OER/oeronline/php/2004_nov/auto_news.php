        take the luxury sport crossover to new levels of refinement. The SRX 
        interior features a new instrument panel, hand-wrapped upper trim, 
        console, centre stack, door trim, wood trim, analogue clock and steering 
        wheel. The redesigned cockpit is a more inviting and luxurious 
        environment, with softer tactile surfaces, new flush-mounted switches 
        and controls, and greater use of wood grain and chrome trim. Real and 
        perceived spaciousness is enhanced via the streamlined new centre 
        console and the addition of a new storage compartment on the passenger 
        side of the instrument panel. The new instrument panel and door trim are 
        built using Cadillac’s new “cut-and-sew” interior process that combines 
        advanced technology with the hand-rendered expertise of expert 
        craftsmen. The vehicle’s new cockpit is hand-built, in the same 
        painstaking fashion Cadillac applies to the limited-production STS-V and 
        XLR-V models, bringing elite craftsmanship into the heart of the luxury 
