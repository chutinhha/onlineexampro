        </font><b><font face="Verdana" size="2" color="#CC3300">TAC on a high</font></b><font face="Verdana" size="1"><br>
        Towell Auto Centre recently won the prestigious individual award in 
        Parts Sales Growth for the year 2006 from Mazda Motor Corporation, 
        Japan. This award was constituted in 2005 for all distributors across 
        the globe, who are evaluated on the basis of set criteria. The common 
        platform for judging the recipient of this award is based on factors 
        like average parts cost of sales per unit in operation, average 
        accessory sales based on new vehicle sales and first-pick parts 
        availability. Towell Auto Centre’s parts sales growth in 2006 has been 
        one of the best in several years, which has resulted in this award. 
        Besides TAC in Oman, Ford Motor Company of Southern Africa (Pty) Ltd. in 
        South Africa also qualified for the award.<br>
