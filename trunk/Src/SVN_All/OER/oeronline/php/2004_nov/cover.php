            <p align="justify"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">Lessons 
            in Leadership</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What Is Your 
            Leadership Style?
            How managers lead in Oman <br>
            OER presents a study, conducted by OCIPED President Salem Ismaily, 
            of leadership styles of managers operating in Oman and the factors 
            that influence these managers in adopting the acquired leadership 
            style</b></font><b><font face="Verdana" size="1"><br>
            <br>
                  </font>





                  </b><font face="Verdana" size="1">
            <img border="0" src="cover1.jpg" align="left" width="215" height="225">Globalisation begets 
            development. It brings with it economic progress and market 
            maturity. And it provides a global scale to otherwise geographically 
            limited businesses. As by-products, globalisation also brings with 
            it the challenges of growth, of efficiency, of productivity, of 
            endurance in the continuously evolving economic ecosystem and, most 
            of all, of maintaining individual and collective identity in an 
            increasingly homogenous environment. And at no other level are these 
            challenges better—or worse—felt than yours: the leadership position.
            <br>
            <br>
            Technology, politics, and economic growth are changing the way the 
            world conducts business under the umbrella of globalisation, 
            resulting in the process of denationalisation of markets, politics 
            and legal systems of the world. Globalisation has been the impetus 
            for mass marketing, multinational corporate power, a new 
            international division of labour, and international mobility of 
            labour, which has reduced the power of nation-states and increased 
            that of the MNCs operating across national boundaries. <br>
            <br>
            The development of the MNC has been an important impetus of 
            globalisation. A product can be assembled from many different units 
            and made in a large number from different countries. As a result, 
            countries of the world have become largely interdependent on these 
            forms of social associations, which stretch across the world. This 
            process has been greatly facilitated by developments in 
            communication and transport. <br>
            <br>
            Although globalisation commenced over five centuries ago when 
            European adventurers started exploring different parts of the globe, 
            the phenomenon has not occurred evenly, and the development of world 
            social relations has resulted in marked inequalities between 
            industrialised and non-industrialised countries often labelled as 
            the ‘Third World’. <br>
            More countries now are members of either international trading 
            bodies or regional economic blocs. The pattern is not limited to the 
            developed industrialised countries. In the Gulf, for instance, the 
            Kingdom of Bahrain, Kuwait, Sultanate of Oman, Qatar, Kingdom of 
            Saudi Arabia and the United Arab Emirates created the Gulf 
            Cooperation Council (GCC), a regional organisation, in May 1981. The 
            GCC Customs Union launched in 2003 and the GCC common currency, 
            envisaged by 2010, combine to offer organisations new opportunities 
            and dimensions for growth, as well as new problems and perspectives 
            to consider. <br>
            <br>
            Being a member of an economic block, countries provide a platform 
            for corporations to operate in large as well as small markets. 
            Economies of scale are achieved when similar schemes of production, 
            marketing and communication programmes are used in several 
            countries. Standardisation of marketing communication can also 
            assist in brand image consistency across markets. Therefore, the 
            economy of member countries will have to be efficient in order to 
            survive in the global competitive markets. <br>
            <br>
            Management represents a capacity for working on and with other human 
            beings in the pursuance of organisational goals; the ability to 
            elicit the active cooperation from subordinate members through the 
            provision of material and symbolic rewards. Management is to do with 
            the application of leadership to the motivation of others. In this 
            context, a manager’s preferences and value systems influence the way 
            in which these causal powers are exercised.<br>
            <br>
            Until recently, mainstream management and organisation studies have 
            largely kept aloof from the debate involving culture and 
            organisational behaviour. Anthropological studies have revealed that 
            the identity of the family and tribal affiliation appears to be 
            central to individual identity and functioning in many traditional 
            societies of the world, including the Arab world.<br>
            <br>
            <b>The milieu</b><br>
            Due to various differences in the cultural background, work 
            experience, level of education and other external influences, 
            managers in Oman may function differently from those in other 
            countries. As Oman is faced with the challenges of globalisation, 
            the situation is conducive to exploring different leadership styles 
            adopted by managers operating in Oman. Although some aspects of 
            culture and its impact on management have been explored in the GCC, 
            there is a dearth of studies from Oman. <br>
            <br>
            Omanis being part of the larger Arab world do have some common 
            values shared by their other Arab cousins. However, Oman also has 
            socio-cultural characteristics that are unique to it. Therefore, 
            Oman offers fertile ground to explore not only leadership styles 
            from the dominant national culture, but also among different ethnic 
            groups within Oman’s subcultures. <br>
            <br>
            Groups of people of different ethnicities, or societies, differ in 
            their values and beliefs, in their aspirations and motivations, and 
            in the ways they satisfy their needs and desires. Such cultural 
            differences pervasively influence all dimensions of human activities 
            and the way international business is conducted.<br>
            <br>
            Understanding the values of the people with whom one is conducting 
            business is an important step in building a good cross-national 
            working relationship. Theoretically, studies from Oman might help to 
            test findings from one cultural group on another in order to test 
            their applicability and universality of the phenomenon and, in turn, 
            this might help to discover phenomena that are not present in one’s 
            own limited cultural experiences. <br>
            <br>
            <b>The paradigm</b><br>
            The increasing development of the Arab world is often considered as 
            unworthy or a passing phase. However, despite these caveats, there 
            are certain characteristics specifically of Oman/Arab approach to 
            management that are worth considering. While distribution of 
            management studies appears to have been of fringe importance in the 
            Arab world, the situation is more acute in Oman.<br>
            <br>
            The Arab leadership style as a unique and untested model might turn 
            out to be a source of inspiration in what is amounting to redundant 
            theories based on Euro-American culture. According to Weir (2001), 
            “Styles and approaches of management in the Arab world may be nearer 
            to the epicentre of this new way of doing things than it has 
            hitherto realised”.<br>
            Examining a multicultural community like Oman might shed light on 
            the current situation in the Sultanate. Information obtained could 
            be used to formulate a sound managerial policy as well as to devise 
            an educational curriculum in order to make the country competitive 
            in the looming age of globalisation.<br>
            <br>
            Emerging studies from the Arab world suggest that organisational 
            behaviour of the Arab institutions might provide a model of society 
            in which family, kin and roles in wider social networks may take 
            precedence over organisational roles and performance. Hence, the 
            Arab management style needs to be further explored.<br>
            <br>
            In addition, while there is a growing interest in the importance of 
            personal relationships in marketing and organisational behaviour in 
            the West, Arabs have been in the forefront on this regard. While the 
            role of relationships has been explored in the Arab world, there is 
            scant attention paid to relationships and leadership style.<br>
            <br>
            Empirical evidence suggests that Arabs conduct their businesses 
            based on personal relationship. <br>
            <br>
            Moreover, when attention is turned to the morality of management, it 
            becomes pertinent again to examine the success of an approach to 
            business in which certain aspects of morality are taken for granted. 
            As in Japan, basic thinking about management in the Arab world 
            starts from an assumption that management and the organisation exist 
            within an envelope of customs and practices, which derive from the 
            acceptance of certain common values. <br>
            <br>
            The morality of management is now at the centre stage of management 
            development itself. It is in this context that the special strengths 
            of management in the Arab world may now become more relevant. Weir 
            (2001) has suggested that Arab management is apparently becoming the 
            “fourth paradigm”.<br>
            <br>
            Managers in Oman may function differently depending on the values of 
            their cultures. It is anticipated that the leadership styles of 
            managers operating in Oman are influenced by their national 
            cultures. Individuals have radically different perspectives towards 
            entering and working in places of different cultural orientations. 
            Ethnic groups, level of education, external exposure and gender 
            significantly influence specifically the management style adopted by 
            managers. <br>
            <br>
            This research examines the role that national culture and 
            subcultures play in influencing the leadership styles of Omani 
            managers. The style and behaviour of indigenous Omani managers is 
            examined in comparison to expatriate managers working in Oman. Other 
            variables such as gender, level and source of education and the 
            organisational position of subject managers is analysed as well.<br>
            <br>
            <b>The conduit</b><br>
            Despite growing interest in international comparative management in 
            recent years, there are few multicultural, empirical studies 
            addressing leadership styles in Arabia and none from Oman. <br>
            <br>
            More research is needed on the effects of ethnic values in general 
            and more specifically, the effect of such diversity on the 
            leadership styles. <br>
            <br>
            Managers operating in Oman come from diverse nations, such as India, 
            Pakistan, the Philippines, Britain, the Netherlands and America. 
            Indigenous Omani managers also come from different ethnic 
            backgrounds, some of them tracing their root elsewhere in Asia. <br>
            <br>
            In addition to being at the crossroads of ancient cultures, Oman is 
            located in the Middle East, which is the centre of old civilisations 
            and the birthplace of the three monotheistic religions. Oman and the 
            surrounding Gulf countries have almost 90 per cent of the world’s 
            proven reserves of oil. <br>
            <br>
            The Omani government has adopted a number of measures to diversify 
            the sources of national income, resulting in its accession to the 
            World Trade Organisation in 2001. Integral to the diversification 
            programme is the privatisation policy, which has seen increased 
            Omani and foreign private sector participation in a number of 
            fields, including electricity generation, water production, seaport 
            and airport development and management among many other privatised 
            projects. <br>
            <br>
            Investment and taxation laws have been updated and government 
            procedures streamlined with the aim of attracting additional foreign 
            capital and expertise. This research aims to take advantage of the 
            presence of diverse ethnic and heterogeneous nationalities within 
            Oman to compare their leadership styles. <br>
            <br>
            <b>The brass tacks</b><br>
            A survey of the available literature suggested that all the studies 
            conducted on leadership styles of the Arab world were conducted 
            using assessment measures developed elsewhere. As for Oman, there 
            are no studies assessing the leadership styles of managers operating 
            in the Oman. <br>
            <br>
            In an attempt to overcome such bias, in this research, the 
            researcher has specifically developed an assessment measure that is 
            sensitive to the local environment. The assessment instrument takes 
            into consideration the soft management factors unique to the Arab 
            world. It also compares Islamic leadership styles such as Shura, 
            Taqleed and Taqleef with others more commonly found elsewhere in the 
            world.<br>
            <br>
            The newly developed instrument was given to various ethnic groups in 
            Oman in order to elicit whether there is ethnic difference in 
            performance of this instrument. The focus of study was to determine 
            the leadership styles of managers operating in Oman and factors that 
            influence them in adopting the acquired leadership styles. The 
            subject—managers operating in Oman—was subdivided further into the 
            following categories:<br>
            1. All managers operating in Oman. <br>
            2. Female managers operating in Oman.<br>
            3. Omani managers operating in Oman.<br>
            4. Managers operating in Oman who have been influenced by:</font></p>
            <blockquote>
            <p align="justify"><font face="Verdana" size="1">• Al Baluchi 
            culture<br>
            • Dhofari culture<br>
            • Lawatiya culture<br>
            • Zanzibari/East African culture</font></p>
            </blockquote>
            <p align="justify"><font face="Verdana" size="1">5. Non-Omani 
            managers operating in Oman. <br>
            <br>
            <b>The Modus Operandi</b><br>
            An electronic questionnaire was sent to 750 senior managers in Oman, 
            of which 51 deleted the mails without opening while 64 opened the 
            mails but did not respond. The data from the remaining 635 
            respondents was then downloaded from the general Web-based journal 
            for statistical analysis. <br>
            <br>
            The respondents were selected randomly and all aspects of the 
            samples in terms of demography, culture, and occupation were 
            compiled to identify the population to which subsequent statistical 
            findings can be generalised.<br>
            <br>
            It is part of an assumption that there will be a number of factors 
            that would influence a manager in adopting a particular leadership 
            style – such as nature of work and employees to be managed. The 
            following factors were considered to have an influence on the choice 
            of the leadership style of a manager operating in Oman:<br>
            1. Manager’s exposure to a particular <br>
            national/ethnic or corporate culture. <br>
            2. Manager’s educational background, whether trained in Oman or 
            overseas.<br>
            3. Manager’s experience in years.<br>
            4. The legal status of the organisation <br>
            the manager works for.<br>
            5. The sector and type of activity of the <br>
            company to be managed.</font></p>
            <p align="justify">
            <img border="0" src="im26.jpg" width="216" height="109"><font face="Verdana" size="1"><br>
            <br>
            <b>The Results</b><br>
            The results of this research indicate that there is a significant 
            difference between what is perceived by the Western world as the 
            Arab leadership style being centralised, autocratic and 
            power-centred. A vast majority of managers operating in Oman employ 
            the Shura leadership style (participative/consultative). This 
            preference of Omani managers may reflect the influence of Islamic 
            beliefs, as Islamic leadership reinforces consultation in conducting 
            all aspects of life. <br>
            <br>
            It is the practice of the traditional Omani society that members of 
            the entire kinship network or community should be consulted on 
            matters important to their welfare. In fact, consultation mechanisms 
            are a particular feature of traditional Omani society. In addition, 
            the participative phenomenon may exist for other reasons. Omanis 
            have been influenced by external forces as a result of many MNCs 
            that operate in the country and the high emphasis placed in 
            education and training conducted overseas. There is a great 
            attention accorded by modern trainers to the philosophy and benefits 
            of a participative leadership style.<br>
            <br>
            Participative leadership style as a modern phenomenon in the West 
            was pioneered by the work of Coach and French (1948) in the US. 
            There has been a groundswell of interest in participative leadership 
            style in American companies. Sashkin (1984, 1986) summarised the 
            need of adopting participative leadership style by stating that such 
            leadership responds to three basic human needs: <br>
            • Increase in autonomy.<br>
            • Increased meaningfulness of work.<br>
            • Decreased in isolation from fellow employees. <br>
            This concept has been exported to other countries doing business 
            with the US. Omani companies through business and training contacts 
            have been importing this concept from the West, reinforcing their 
            own Islamic teachings.<br>
            <br>
            The other reason that managers in Oman most commonly use this 
            concept is that Omanis are more oriented to individuals rather than 
            papers. Individuals in Oman are trained since childhood to 
            communicate with others through social ceremonies and other affairs. 
            Oman as a country has three sides of its boarder surrounded by the 
            sea, making it almost like an island. As it is the first port of 
            call to the GCC countries, historically, all visitors to the Gulf 
            had to enter through Oman. Therefore, Omanis have had to develop 
            skills to communicate with other people who constantly called on her 
            shores. <br>
            <br>
            Oman has continued to invest revenues from its hydrocarbons 
            resources in economic development projects and, as a result, has 
            attracted a number of expatriate workers to take up employment in 
            the country. The total labour force employed in Oman’s private 
            sector is estimated to be 550,000 (Ministry of National Economy, 
            2004). These workers are engaged in all economic sectors of the 
            country. They come with diverse work experiences as well as from 
            diverse nationalities of different cultural backgrounds.<br>
            <br>
            While Omanis make up 35 per cent of the labour force employed in the 
            private sector, the expatriate workers hold 46 per cent of the 
            leadership positions. With such a big influx of expatriate workers, 
            it is assumed that the corporate culture of managers operating in 
            Oman will reflect that of a heterogeneous culture. As the results of 
            this research shows, 69 per cent of Omani managers use predominate 
            Shura leadership style, which is similar to that adopted by the 
            expatriate managers (79 per cent).</font></p>
            <p align="justify">
            <img border="0" src="im27.jpg" width="220" height="174"><font face="Verdana" size="1"><br>
            <br>
            It is also worthwhile to note that about 79 per cent of non-Omani 
            managers have spent more than 10 years of their professional lives 
            in Oman. It can be argued that spending more than 10 years in a 
            country is enough time for any manager to acquire the prevailing 
            culture of the host country. It is also possible that some of these 
            expatriate workers come from countries of similar cultures as those 
            found in Oman.<br>
            <br>
            <b>Ethnic influence</b><br>
            In general, studies on cross-cultural management deal with 
            traditional organisational behaviour issues such as motivation, 
            leadership, decision-making, and group dynamics. It covers 
            descriptions of the behaviour of people from different cultures 
            working within a single organisation and compares the behaviour of 
            people in organisations located in different cultures. These 
            groupings may be called tribes or races and have a common 
            identification based on language, ethnicity, culture, religion or 
            profession (Ann Marie, 2000). <br>
            <br>
            Factors considered to have a major impact in the formation of the 
            Omani national culture and subcultures are: <br>
            • The family<br>
            • Tribe<br>
            • Religion <br>
            • Other external influences<br>
            <br>
            This study basically compares the leadership styles of various 
            managers working in the Sultanate with more emphasis on effect of 
            the Omani sub-cultures have to the leadership Styles, among other 
            variables. Out of the respondents 26 per cent of them indicated that 
            they were exposed to Zanzibari/East African culture and 29 per cent 
            were others. The majority of the respondents used Shura 
            (Consultative/ Participative) style of leadership as their dominant 
            style.</font></p>
            <p align="justify">
            <img border="0" src="im28a.jpg" width="290" height="338"></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im28b.jpg" width="225" height="284"></font></p>
            <p align="justify">
            <img border="0" src="im29.jpg" width="222" height="132"><font face="Verdana" size="1"><br>
            <br>
            <b>Training &amp; education</b><br>
            One of the studies on the Arab managers was that undertaken by MEIRC 
            (Muna 2003). The authors identified five main ingredients for 
            success among managers in the GCC: a good educational head-start, 
            early exposure to successful role models, early responsibilities in 
            the home or around it, the importance of an ethical system which 
            puts a high value on hard work and commitment, and the need to take 
            one’s own initiatives in self development. <br>
            <br>
            According to this study, three times as many managers in the GCC 
            have university degrees compared with their counterparts in the US, 
            UK, France, Germany or Japan. Organisations in the GCC spend three 
            times as much money and time on their management development every 
            year than do their counterparts in the UK. <br>
            <br>
            The leadership style commonly used by the Gulf managers, as 
            identified by the MEIRC study, was consultative decision-making 
            process, similar to that referred to as Shura in this research.<br>
            <br>
            The emphasis on education, training and management development also 
            ranks high on the Omani agenda. Most of such training is achieved by 
            utilising expertise from Western technology and local management to 
            Western corporate culture. Oman has been in the forefront in 
            pursuing the process of localisation of manpower. <br>
            <br>
            Through the political requirement of replacement of ex-patriot 
            managers with nationals, more local managers now take leadership 
            positions in Omani corporations. <br>
            <br>
            The results of this research show that managers operating in Oman, 
            including the Omani indigenous managers, are highly trained 
            managerial cadres. More than 89 per cent of all managers operating 
            in Oman are university graduates, and among the Omani managers, 85 
            per cent have university degrees. <br>
            <br>
            The results of this research on managers operating in Oman are 
            similar in many ways to the one cited above. Managers operating in 
            Oman predominately use the Shura leadership style, similar to the 
            one identified by the study referred above. <br>
            <br>
            <b>Work Experience</b><br>
            A focus group discussion was conducted to obtain first hand 
            information from managers and experts in the field of leadership 
            styles. The group considered that the leadership style a manager 
            uses is also sensitive to the number of years in experience of his 
            employees. As companies constantly have to face different challenges 
            such as employee turnover, the managers will always have to lead 
            employees at different levels of competency. <br>
            <br>
            While managers could consult with more experienced employees instead 
            of adopting an authoritative style, they cannot completely delegate 
            the work to inexperienced new recruits. Even new recruits with 
            experience have to unlearn their old styles of leadership so they 
            can blend in with the new company’s style.<br>
            <br>
            There are always changes in terms of new strategies, mission 
            statements and even leadership at the height of the hierarchy of 
            companies. With such changes, the companies might be obliged to go 
            through transition in leadership and hence the manager also might be 
            required to change the leadership style to fit the company’s new 
            image and mission. <br>
            <br>
            The results of this research confirm such an argument indicating 
            that, at any given time, the managers operating in Oman use at least 
            three leadership styles with one of them most dominant.<br>
            <br>
            <b>Nature of organisation and sector of activity</b><br>
            The focus group discussion also considered that the legal status of 
            a company could influence the type of leadership to be used in 
            managing the company. If a manager were to lead a public 
            shareholding company and according to the laws and regulations 
            enforced by the Capital Market Authority in Oman, the company is 
            required to have stringent cooperate governance. The manager, 
            therefore, is required to lead the company through proper checks and 
            balances with the involvement of most of the stakeholders.<br>
            <br>
            Then again, managers who lead closed family-owned companies have 
            only the family to report to. As is the practice in some companies 
            in Oman, the managers themselves mostly come from the same families 
            that own the companies. As the company or the manager is not liable 
            to report to any other stakeholders other the owner, the leadership 
            style of these companies is assumed to be highly centralised and 
            autocratic. <br>
            <br>
            On the other hand, if a manager is leading a company that places a 
            paramount importance to the safety and security of its employees and 
            customers, such as in the oil &amp; gas sector, the manager would find 
            it necessary to provide a regular monitor to ensure that the 
            procedures, guidelines and rules and regulations are adhered to.<br>
            <br>
            As for a manager who leads a company that is in knowledge-intensive 
            activity, such as information technology, most probably, the 
            employees have more knowledge of how the work is to be done than the 
            manager. It is, therefore, necessary for the manager to delegate a 
            major part of the work to the employees. <br>
            <br>
            The focus discussions group also highlighted that a situation to be 
            managed dictates the style of leadership to be adopted. When a 
            manager has to handle emergency situations, such as a fire-fighting 
            team, the manager cannot sit and consult his team on what to do when 
            a fire breaks out. The manager would need to use his/her authority 
            in getting his team immediately to attend the fire. Therefore, the 
            best leadership style that the manager would use in such a situation 
            would be that of directives/autocratic. <br>
            <br>
            The results of this research, confirms the arguments put forward by 
            the focus discussion group. None of the managers in family-owned 
            companies were found to use delegative leadership style while 
            majority of managers of public shareholding companies use the more 
            open style of Shura.<br>
            <br>
            <b>Assimilation and acculturation</b><br>
            The findings of this research collectively indicate that the 
            behaviour of managers operating in Oman have assimilated well into a 
            cosmopolitan culture provided by the multiracial managers that 
            operate in the country. Moreover, these managers have adapted well 
            to the new culture. This is evident by the results of this research: 
            a majority of the managers use the more open interactive leadership 
            style through consultation.<br>
            <br>
            Assimilation is the process by which an individual develops a new 
            cultural identity. The underlying assumption of all the assimilation 
            models is that a member of one culture loses his/her original 
            cultural identity as he/her acquires a new identity in a new 
            culture. There is one major danger associated with assimilation: the 
            possibility of being rejected by members of the majority culture. 
            Due to the diverse and mix culture of Oman, the effect of this 
            danger is minimal. <br>
            <br>
            The acculturation model is similar to the assimilation model in 
            three ways. They both:<br>
            a) Focus acquisition of the majority group’s culture by members of 
            the minority group <br>
            b) Emphasise a unidirectional relationship between the two cultures
            <br>
            c) Assume a hierarchical relationship between the two cultures<br>
            <br>
            What differentiates the two models is that the assimilation approach 
            emphasises that individuals, their offspring or their cultural group 
            will eventually become full members of the majority group culture 
            and lose identification of the culture of their origin. By contrast, 
            the acculturation model implies that the individual, while becoming 
            a competent participant of the majority culture, will always be 
            identified as a member of the minority culture.<br>
            <br>
            More than half of the respondents were middle and senior level 
            managers while just 6 per cent were at non-managerial positions. As 
            it is not the intention of this research to study non-managerial 
            samples, the research instrument was not sent to non-managerial 
            samples. It is possible that these samples received the survey 
            instrument forwarded by others who participated in the research. A 
            small number of about 8 per cent did not disclose their position 
            while around 2 per cent fell into the category of “Not Applicable” 
            either because they do not fall into any of the positions mentioned 
            in the instrument or would also not wish to reveal their position.<br>
            <br>
            <b>The gender factor</b><br>
            As the world faces new and age-old challenges to development and 
            peace, it is crucial that women gain and fully exercise leadership 
            roles at many levels in business, government, education, and civic 
            life in their countries. Building a leader begins at home and the 
            family must believe in the ability of women to be successful (Al 
            Anjari, 2004). Omani women no longer have to fight to reach their 
            goals; they are supported to reach them from their homes. <br>
            <br>
            The government of Oman has given Omani women equal rights in 
            education and employment. Women form almost 49 per cent of the Omani 
            population (Ministry of National Economy, 2004) and provisions have 
            been made in the Personal Statue Laws (PSL), granting equal rights 
            to women. The equal opportunities to women are given at the basic 
            level with equality in education with girls dominating the ranks in 
            higher education. Among the women managers operating in Oman 
            surveyed, 84 per cent of them have completed university education.
            <br>
            <br>
            The progress made by women in Oman is reflected not only in the 
            numbers employed but also the leadership positions they hold in the 
            government as well as the private sector. In fact, Oman was among 
            the first GCC states to have a woman minister, a member of State 
            council, a member of consultative council, a member of the Board of 
            Directors of the Chamber of Commerce and Industry and even the first 
            and only woman president of a sports club in the region. <br>
            <br>
            The results of this research indicate the percentage of women in 
            leadership positions in Oman is 21 per cent. Women’s participation 
            in managerial and administrative posts is around 33 per cent in the 
            developed world, l5 per cent in Africa, and 13 per cent in Asia and 
            the Pacific. In Africa and Asia-Pacific, these percentages, small as 
            they are, reflect a doubling of numbers in the last 20 years (Women 
            Leading Partnership, 2004). Oman’s position is second only to 
            countries in the developed world such as Scandinavia and North 
            America (Japan is a developed country, yet it is lagging behind).<br>
            <br>
            The results of the research have not shown any particular pattern 
            for woman managers operating in Oman. The leadership styles adopted 
            by women managers show the same patterns as those adopted by males. 
            Probably one of the reasons that led to these results is that women 
            are exposed to the same process of acculturation and assimilation as 
            their male counterparts. Furthermore, female managers belong to all 
            categories of ethnic cultures and hence their influence and exposure 
            is representative of the general sample.<br>
            <br>
            A total of 135 women responded to the survey, of which a majority 
            (84 per cent) were graduates and postgraduates with 63 per cent of 
            them having studied overseas with more than 11 years of experience. 
            More than one-third of them work in the finance sector, mostly in 
            public shareholding companies. Sixty Nine per cent of the surveyed 
            women managers in Oman use Shura as their dominant leadership style 
            and 16 per cent use the Taqleed style.
