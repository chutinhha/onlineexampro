<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Marketing 
Guerrilla Style</font></font><font face="Verdana" size="1"><br>
<br>
<b>Matt Harrison explains how small business owners can benefit from this cheap,&nbsp; 
but effective, marketing approach</b></font></p>

<p align="justify"><font face="Verdana" size="1">For many small Oman-based 
firms, marketing is too often seen as an expensive and time-consuming process. 
Add to that the lack of marketing know-how, and it’s easy to understand why many 
of our small firms go under in their early stages. Therefore, it’s important we 
appreciate that marketing is one of the main forces that drives businesses 
forward. In fact, it’s critical that small (as well as large) businesses use it 
effectively.<br>
<br>
‘Guerrilla Marketing’ is a relatively new approach that offers the small 
business owner a cheap and, often, more successful campaign than common 
marketing strategies such as newspaper ads, mail shots and telesales. As a 
result, guerrilla marketing has lead people to think ‘outside the box’ to market 
their business more effectively with, usually, limited resources of cash and 
staff. It’s cheap, smart and it can really get a small firm a lot of publicity.<br>
<br>
First things first, what exactly is guerrilla marketing? It’s about being 
original, breaking the rules and looking for alternatives. Today, the term 
covers a multitude of marketing techniques but I like this particular 
definition: ‘‘Guerrilla marketing is unconventional marketing intended to get 
maximum results for minimal resources.’’ Put simply, it means marketing that is 
non-traditional and not playing it by the book. <br>
<br>
Here are 10 ways of looking at it:<br>
1. Instead of investing money in the marketing process, you invest time, energy 
and imagination.<br>
2. Instead of using guesswork in your marketing, you use the science of 
psychology and laws of human behaviour.<br>
3. Instead of concentrating on traffic, responses, or gross sales, profits are 
the only yardstick by which you measure your marketing.<br>
4. Instead of being oriented to companies with limitless bank accounts, 
guerrilla marketing is geared to small business.<br>
5. Instead of ignoring customers once they’ve purchased, you have a fervent 
devotion to customer follow-up.<br>
6. Instead of intimidating small business owners, guerrilla marketing removes 
the mystique from the entire marketing process, clarifies it.<br>
7. Instead of competing with other businesses, guerrilla marketing preaches 
cooperation, urging you to help others and let them help you.<br>
8. Instead of trying to make sales, guerrillas are dedicated to making 
relationships; long-term relationships are paramount.<br>
9. Instead of believing that single marketing weapons such as advertising work, 
guerrillas know that only marketing combinations work.<br>
10. Instead of encouraging you to advertise, guerrilla marketing provides you 
with 100 different marketing weapons; advertising is just one of them.<br>
<br>
We’re bombarded with TV, radio and newspaper ad campaigns 24x7. They’re part of 
our lives, so much so, they’ve simply become background noise. Therefore, 
marketers increasingly have to resort to guerrilla tactics to grab our 
attention. Let’s look at some classic guerrilla marketing tactics that you might 
want to experiment with.<br>
<br>
<b>Mad campaigns</b><br>
IBM employed a graffiti artist to paint pavements and walls in San Francisco and 
Chicago with the words ‘Peace, love and Linux’. The site About.com sent out fake 
hitchhikers onto Highway 101 in the States to give out promotional masks to any 
kind driver that stopped to pick them up. Both campaigns focused on creating a 
buzz around a product or service, it’s then passed on by word of mouth, and in 
marketing, word of mouth is paramount. According to a May 2001 McKinsey report, 
67 per cent of US consumer good sales are influenced by word of mouth. If one 
person has a great brand experience, they can influence five to seven other 
people. <br>
<br>
<b>Free Swag!</b><br>
Stationery, key rings, software, T-shirts, postcards, posters… if it’s free, 
we’ll take it – even if it’s something we don’t really need. Everybody loves a 
freebie and as long as it’s relevant, it can help embed your company name and 
URL into the memory banks of potential customers. For example, Pets.com handed 
out branded tennis balls and dog biscuits to dog owners at parks around the US. 
This is a perfect campaign tightly focused on the right market (i.e., pet 
owners) and with freebies they’ll find genuinely useful. <br>
<br>
<b>A Helping Hand</b><br>
To get publicity for gift registry site Della.com, Ammo Marketing sent out ‘wish 
fairies’ that granted random acts of kindness to shoppers, such as paying 
parking costs and carrying their shopping. It was a simple idea, but it brought 
in a huge amount of publicity for the company, and the resulting coverage on TV, 
radio and in print was valued at a $3 million (RO1.15 million). The traffic to 
the Della.com increased by over 100 per cent too. The promotion worked so 
successfully because it was well focused – Della.com’s aim was to take the 
stress out of Christmas shopping for consumers and that’s exactly what the ‘wish 
fairies’ were doing.<br>
<br>
<b>Go a Step Further</b><br>
To really push home that marketing message, you could always employ somebody to 
hand out postcards for you. Ammo Marketing hired a good looking woman to hand 
out what looked like business cards to young men in a bar, they thought they 
were getting her phone number, when in fact the card had details of Electronic 
Arts’ new online game. It’s a bit naughty, but it was targeted at the right 
audience (young males) and the response rate was an impressive 60 per cent.<br>
<br>
<b>Courtesy and Follow-up Calls </b><br>
It’s a fact that businesses lose customers because they don’t pay them enough 
attention. Once a customer has made a purchase from your business, your aim 
should be to get them back again and again. Research shows that the cost of 
selling to a new customer is five times more than selling to an existing 
customer. <br>
<br>
It’s fair to say that it’s hard to track all customers because you may not have 
their contact details. For those that you do have contact details, send them a 
compliments slip or give them a phone call asking them if they are happy with 
their purchase and if you can offer any further assistance. This should be done 
within, say, the first 30 days post-sale and then perhaps every 60 days after, 
until you have reason to believe that the customer has no real intentions of 
making a repeat sale. <br>
<br>
<b>Bring Customers to Your Website</b><br>
Getting new people to come to your Website can be difficult. Sometimes, it may 
call for you to try alternative and new ideas for getting people’s attention. 
The first idea would be to introduce an online game to your Website that people 
will find addictive if nothing else. If you can find a game that’s relevant to 
your business, even better. <br>
<br>
This approach is more likely to attract younger people, so be sure that they are 
included in your target audience before adopting the idea. Although people will 
be pulled in by the entertainment, most people may decide to browse your site 
while they are there. <br>
<br>
Try adding humour to your site, without offending people of course, as customers 
always welcome that extra incentive to look at somebody’s site. Keep the humour 
relevant to the content and don’t go too far with the jokes, as they may 
distract people from actually buying something. You can offer free downloads 
even if it isn’t entirely relevant to your business. For example, desktop 
wallpaper, screensavers, images, etc.<br>
<br>
<b>E-mail </b><br>
Have you ever received a funny e-mail and forwarded it to one of your friends? 
Just imagine how many people that e-mail has been to and the amount of time that 
it has been around for. Wouldn’t it be brilliant if a similar e-mail were 
distributed the same way with your Website address included? If you have a funny 
photo, joke or image, again without being offensive – send it out. It’s more 
likely to be appreciated if it relates to recent events in the news: timing is 
important. <br>
<br>
<b>Newsletters </b><br>
Write a weekly or monthly newsletter and give people the opportunity to 
subscribe. The newsletter, if informative and useful, will be appreciated by 
visitors and for those that subscribe, often will forward it to friends, family 
and work colleagues via e-mail. The idea is to keep customers updated about your 
business such as new products, services, information, news, etc. It should then 
refer customers back to your Website for further detail.<br>
<br>
<b>Cover your Firm’s Cars</b><br>
Get your URL plastered across your company car, at least your name will become 
known in your local area. The ill-fated Web currency company beenz went to the 
expense of getting a bullion truck with its URL emblazoned across it and then 
parked it in front of the offices of national newspapers and at key events, such 
as the annual Economist e-business conference. <br>
<br>
<b>Wear the Shirt</b><br>
Josh Silverman, the CEO of online activity planning site Evite.com, is always on 
duty. He wears his Evite.com bowling shirt when he knows he’s going to be seen 
in a club or restaurant. And he always carries Evite.com branded pens with him 
so he can leave them around after he’s signed his credit card bill. This is a 
very cheap form of guerrilla marketing, and if you really believe in your 
company, you should be proud to wear the T-shirt. <br>
<br>
<b>Talk to Journalists</b><br>
Be controversial and quotable, without getting yourself into hot water. Phone 
Oman-based journalists with comments on relevant news stories and to offer 
pro-active comment whenever your competitors make an announcement. This doesn’t 
necessarily need to be aggressive, but it should provide an alternative 
viewpoint.<br>
<br>
<b>Wrap Up</b><br>
Seth Godin said: “We live in a world where consumers actively resist marketing. 
So it’s imperative to stop marketing at people. The idea is to create an 
environment where consumers will market to each other.’’ Indeed, any small or 
early stage company has to take a guerrilla approach to marketing if it’s to 
generate a profile for itself. Let’s face it, no one takes any notice of a small 
business that pumps out the occasional press release. In brief, isn’t it about 
time you tried some the guerrilla ideas fleshed out above? And when you do, let 
me know how they go.<br>
