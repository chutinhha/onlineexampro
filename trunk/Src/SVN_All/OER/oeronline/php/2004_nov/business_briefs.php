Challenge’, is endorsed and supported by
the Omani government, who will be marketing
the event to promote the country
as an adventure tourism destination via a
website and targeted media and advertising
initiatives in the Gulf region and the
UK. “The other significance of this sea
adventure is that it will attempt to raise
funds for the Oman Cancer Group, the
only group of its kind in Oman which is
spreading cancer awareness in the country,”
Mark said. The well-known British
adventurer is set to paddle around 1,700
km of coastline to highlight Sultanate’s
great tourism potential and to raise funds
for Oman Cancer Group. This adventure
trip gains great significance, for it is
almost timing with His Majesty Sultan
Qaboos’s recent statement where he had
stressed on raising Oman's tourism
potential. His Majesty the Sultan had
underlined the need to take advantage of
the natural diversity and geographical
location of the Sultanate, by way of
tourism to add to the national revenue.
This journey has all the potential to bring
the Sultanate out into the open as a country
of great tourism potential, Mark and
the companies sponsoring this trip said.

