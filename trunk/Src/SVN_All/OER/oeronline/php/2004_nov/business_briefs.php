agreements Omantel would soon sign to
introduce the electronic collection system.
Initially, 10-bill payment machines
(BPM) will be installed all over the
Sultanate to help customers pay telephone
invoices at time of their convenience.
Wohaibi stressed that the introduction
of the electronic collection system
through the 67 branches of Onec
throughout the Sultanate falls within
Omantel’s ongoing efforts to facilitate
bill payment and make the process easier
and faster. In a statement to the press
following the signing ceremony,
Wohaibi hoped the new initiative would
address payment snags some customers
were facing in the past. Further facilitating
procedures will follow soon, he
added. The Omantel executive president
also said the new billing system is one of
a series of confidence-building procedures
the company adopts with the ultimate
goal to enhance cooperation and
strengthen relationships with customers
wherever they are.

