<p align="justify"><font face="Verdana" size="1"><b>Royal Decree sets up
Ministry of Sports Affairs
</b><br>
His Majesty Sultan Qaboos bin Said has
issued the Royal Decree No. 112/2004
which establishes a Ministry of Sports
Affairs and specifies its prerogatives and
Royal Decree No. 113/2004 which
appoints Eng. Ali bin Masoud bin Ali Al
Sunaidi as Minister of Sports Affairs.
Article one of the decree says that a
Ministry of Sports Affairs shall be established
to which the prerogatives, allocations
and assets relating to sports activities
at the General Organisation for Youth
Sports and Cultural Activities (Goysca)
shall be transferred. All employees of the
sports activities at the Goysca shall be
transferred to the new ministry with their
current job status. Article two states that
the prerogatives, allocations and assets
relating to cultural activities shall be transferred
from the Goysca to the Ministry of
Heritage and Culture. All employees of
cultural activities at the Goysca shall also
be transferred to the Ministry of Heritage
and Culture with their current financial
grades. Article three cancels Royal Decree
No. 113/91, which established the Goysca
and Royal Decree No. 67/94, which
endorsed the Goysca organisational structure.
It also cancels all that contravenes the
provisions of this decree.

</font></p>

