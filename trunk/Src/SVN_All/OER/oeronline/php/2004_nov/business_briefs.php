<p align="justify"><font face="Verdana" size="1"><b>BankDhofar 9-month
net profit rises 18 pc
</b><br>
The impact of erstwhile Majan
International Bank’s merger with
BankDhofar has started reflecting in
the books of the latter. BankDhofar
reported nearly 18 per cent jump in net
profit for the first nine months of the
current year ended September 30, 2004.
The net profit rose from RO7.087 million
in September 2003 to RO8.351 million
in September 2004. When most
banks post fall in loan book size,
BankDhofar shows signs of continuous
increase. Loans and advances witnessed
an increase of 6.5 per cent at
RO378.66 million as against RO355.65
million as of September-end 2003. Of
course, fall in loan book size, in the
case of some banks, was because of
cautious lending policies. The increase
in profitability can be attributed to the
9.5 per cent increase in gross interest
income and a 10.8 per cent increase in
net interest income, despite a 5.3 per
cent increase in interest expenses. Total
assets registered an increase of 7.45 per
cent at RO485.28 million compared
with RO451.617 million.

