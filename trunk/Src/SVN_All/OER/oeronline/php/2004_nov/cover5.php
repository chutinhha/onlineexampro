            <img border="0" src="im38.jpg" align="left" hspace="5" width="250" height="135">What 
            is your leadership style?</b><br>
            Participative and Delegative. I try to understand every detail of 
            the business and the customers’ requirements, and then prepare the 
            action-plan for every business, based on the resources available and 
            my experience. I just don’t simply delegate a task without first 
            understanding the nitty-gritty. <br>
            <br>
            I live that life before I expect my staff members to do it. 
            Procedures are meant to be set after experience. They are to be 
            documented for clarity. I make my subordinates understand my 
            expectations, and then guide and monitor them throughout the 
            process, whenever required. I give them the freedom to operate, and 
            step in only when absolutely necessary.<br>
            <br>
            <b>What factors have impacted your leadership style?</b><br>
            Family, work experience, education and training. I hail from a 
            business family – so, from my childhood, I have been exposed to an 
            entrepreneurial mindset. I began my career at an age of 17, and I am 
            today 50 years old. So, this large experience obviously has had an 
            impact on my leadership style. <br>
            <br>
            <b>What are you doing today to develop leadership within your 
            organisation?</b><br>
            We have identified the second and third line of managers. I believe 
            in giving my team members the freedom to think differently, because 
            that is what leads to better products. My strategy is not to find 
            faults, but to help correct them. I always try to develop a staff 
            member for taking bigger responsibilities. I give them full 
            confidence in their own abilities. Above all, I always encourage 
            employees to come to me when they have a problem.<br>
            <br>
            <b>If you could teach one thing about ‘leadership that drives 
            growth’ to new employees, what would it be?</b><br>
            Have an ambition in life. Every one should know what they should 
            become in life, and then go all out to achieve it.<br>
            <br>
            <b>What strengths do you rely on most in your daily life as a 
            leader?</b><br>
            Positive attitude. I would never give up any challenge without 
            trying because it is ‘impossible’. I will always look for and find 
            ways to achieve my target. Another of my strengths is that I make 
            periodic corrections to my approach. I always accept my mistakes and 
            ensure that they are not repeated.<br>
            <br>
            <b>How do you align individuals’ expectations with your 
            organisational strategy?</b><br>
            It’s actually very simple – make the individual realise their market 
            worth, and make sure that you are not underpaying them. Share with 
            him the growth plan. My staff is clear about the structure of the 
            company so that they know how they move forward. I also share with 
            them our limitations and positives. Above all, I ask them to give 
            their suggestions on solving their issues, and sometimes that really 
            works.<br>
            <br>
            <b>When selecting someone to join your team, what qualities will you 
            seek? </b><br>
            The person must have life in him. There should be light in his eyes. 
            I like people who are bold and who can think different. I like 
            people who ask more questions during the interviews than I do. <br>
            <br>
            <b>Do you enjoy taking risks?</b><br>
            Yes, but calculated risks. There are things that are beyond your 
            control. I evaluate the risk and quantify the possible loss that 
            would occur if things were to go out of control. I personally 
            monitor every step in any business where there is risk. The best 
            approach is to avoid confrontation and as far as possible resolve 
            issues amicably.<br>
            <br>
            <b>What is your greatest professional satisfaction?</b><br>
