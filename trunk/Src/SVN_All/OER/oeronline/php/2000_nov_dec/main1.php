                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Keeping Stock</font>
                    <p><font face="Verdana" size="2">
                    <img border="0" src="../../images/2000_nov_dec/keep_stock.jpg" align="left" hspace="5" vspace="5" width="200" height="150"></font><font face="Verdana" size="2">The Sultanate's fisheries cannot afford to be complacent, warn experts. They say waste, misuse and over-harvesting of certain species such as kingfish and shark from traditional and commercial fishing exceeds sustainable levels and welcome government action to rectify the inbalance.&nbsp;<br>
                    <br>
                    "The species in noticeable decline include kingfish, lobster, shrimp, abalone and shark," says Dr Hamed Al-Oufi, an SQU lecturer in the Department of Marine Science and Fisheries and Oman's first Phd graduate in Fisheries Management. He identifies many possible reasons for the over-exploitation of these species, such as increased domestic population growth, the rise in international demand for fish generally and the simple fact that selling fish abroad has become more lucrative. He also says that improvements to the internal market infrastructure as well as developing fishing technologies coupled with factors such as natural mortality and environmental conditions have slowed these stocks from recovering to original levels.<br>
                    <br>
                    "As in most developing nations, certain stocks of Oman's coastal fisheries have become characterised by over-exploitation, with a consequent depletion of stocks and reduction in the prosperity of the coastal population who are solely dependent upon fishing for their livelihoods," notes a recently published Sultan Qaboos University (SQU) report by the Department of Marine Science and Fisheries. "Landings of high-value and favoured species by Oman's artisinal industry have, since the mid-1980s declined, although increased catches of other species have tended to offset value and yields."<br>
                    <br>
                    A current joint project between SQU and the Marine Science and Fisheries Centre is studying the problem of declining kingfish stock and is mapping out a fisheries management plan to revive their numbers along the entire coastline of Oman. This research will be completed by December 2002.&nbsp;<br>
                    <br>
                    Indeed, SQU is undertaking a number of research projects awarded by the government including an OR160,000 three-year project initially focusing on the use of fisheries by-catch as aquafeed ingredients. Experts suggest that the processing of by-catch could ultimately be used in human and livestock diets if sufficient standardisation takes place to provide food that has enough high-quality protein.&nbsp;<br>
                    By-catch is a term used for fish caught but then discarded of by fishers who focus on specific species to sell, usually those that attain the highest commercial value in export markets. By law, by-catch in Omani waters from commercial trawlers must be brought to port in Oman but analysts point out that this is difficult to enforce. Moreover, it is estimated that on average, close to half of any given commercial landing is by-catch.<br>
                    The provision of over 15 sea ports by Oman's government during the past decade has made it easier for the by-catch from foreign demersal trawlers and longliners, which dominate the commercial fisheries, to reach local markets. These sea ports have also ensured that the quality of the catch landed by traditional fishers is improved. As well as offering ice production and fuel bunkering facilities, the ports provide a necessary Hazard Analysis Critical Control Point (HACCP) to record data and to monitor fishing activities more easily.&nbsp;<br>
                    <br>
                    Industry sources say there is a great potential for aquaculture and fish farm development over the next decade. With over eight plans drawn out (as per studies funded by the Ministry of Agriculture and Fisheries since 1981) like the abalone master plan in Salalah or breem farming in Bandah Khiran.&nbsp;<br>
                    <br>
                    Meanwhile, a five-year government sponsored program called 'The Shark Project' has been initiated to develop a national plan of action to protect and manage sharks against directed fishing. Experts argue that the finning of fish (a malpractice that involves only harvesting the fins of the shark and dumping of the carcass into the sea) could decimate shark stocks and blame the phenomenon on huge demand for shark fins in export markets like China.&nbsp;<br>
                    <br>
                    "Those who conduct such activities need to be made aware that shark products are very valuable. The entire carcass can be dried for food and there is such an industrial unit in Sur that processes shark meat. The pharmaceutical industry can utilise the cartilage as an ingredient for anti-cancer drugs and the flesh contains other agents which can be applied to manufacturing medical products," says Dr Ewen McLean, head of SQU's fisheries science and technology department.&nbsp;<br>
                    <br>
                    The conclusions to one SQU report (Al-Oufi et al., 2000) based on the observations upon the Al-Batinah artisinal fishery, says that the centralisation of the fishing industry away from local systems can result in dramatic failure. It states: "To a certain degree, the Al-Baatinah fishery experiences this situation, where norms are disregarded due to entrants failing to obey Senat Al-Bahar tradition ['code of the sea']. Examples here include the use of illegal gears, fishing in restricted areas and increasing fishing effort. In turn, this has had the knock-on effect of disrupting the behavioural patterns of established fishermen, some of which employ illegal mesh sizes, poach and fish in prohibited areas. Clearly, a critical component for the success of co-management strategies in the Al-Batinah region must be a more clear-cut division of responsibility."<br>
                    <br>
                    As per the United Nation's Code of Conduct for Responsible Fisheries, adopted by the Sultanate in March 1999 and the Indian Ocean Tuna Commission (April, 2000), Oman is obligated to fulfil and implement a framework of rules to ensure the country is successfully managing its coastal resources. Laws are already in place that prevent egg-producing lobsters being fished as well as spearfishing and the collecting of live coral. Restrictions also apply to the use of gillnets as well as harvesting during spawning seasons.&nbsp;</font></p>
