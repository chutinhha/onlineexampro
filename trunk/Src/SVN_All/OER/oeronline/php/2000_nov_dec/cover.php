                    <p><font face="Verdana" size="2">Revenue from fisheries currently contributes some RO58.7 million annually to the Sultanate's GDP. By 2005, this figure is forecast to jump by more than 55% to RO91 million. According to Minister of Agriculture &amp; Fisheries, His Excellency Dr Ahmed Bin Khalfan Al Rawahi, this growth will also mean that the number of those employed directly in fishing as well as associated industries will increase substantially, providing hundreds of new jobs and business opportunities for young Omani graduates.<br>
                    <br>
                    "Fisheries is very important to Oman. There are over 27,000 Omanis employed as fisherman and more than 4,000 Omanis working in associated industries such as transportation and trading. The Ministry of Agriculture &amp; Fisheries is very interested in developing this sector, Dr Al Rawahi says.<br>
                    <br>
                    "What we are hoping in the coming 6th Five-Year Plan is to focus on four main objectives. The first is to establish a Fishing Code of Conduct." Dr Rawahi says this will be developed so it meets the standards of other similar international codes of conduct; the main aim of Oman's code being one of sustainable development. To ensure the principles embraced in the code are achieved, wider legislation will be introduced to protect fisheries resources, including increased surveillance and enforcement - from on the water to the borders - of such laws. Dr Rawahi says it is hoped that the amended Fishing and Protection of Living Sea Resources Act will come into force sometime in 2001.&nbsp;<br>
                    <br>
                    The second objective is to strengthen the role of fisheries as a contributor to the national economy by increasing production as well as returns from fisheries exports (currently 32% of the Sultanate's catch is exported to 44 countries) through improved quality controls and efficiencies and the introduction of new technology. The third objective is to continue to improve the standard of living and retirement for fishermen and the growing number of women who work in associated industries, while the fourth is to increase the contribution of fisheries to Oman's total food bill.&nbsp;<br>
                    <br>
                    In addition to growing production levels according to principles of sustainable development, the ministry has invested some RO3.162 million in researching, assessing the viability of, and developing 13 projects which encompass everything from aquaculture (abalone beds off the coast of Dhofar; a $US32 million joint Omani-US shrimp farm project at Duqm in Al'Wustah which will produce 4,000 metric tonnes of shrimp per year; two fish farms producing species such as sea bream and hammour in the Musandam , as well as one at Qalhat near Sur in Sharqiya and one at Quriyat) to spin-off industries such as the production of fish food from local resources for the fish farming industry. In addition to these ministry-led projects which will be phased in over the next three to four years, Dr Al Rawahi says the ministry is helping with the establishment of a number of other new businesses starting up in support industries such as canning and processing for export and the manufacture of fishing equipment.&nbsp;<br>
                    <br>
                    "By realising many of these projects and seeing them working, society will see exactly what we mean by fisheries resources becoming a major employer of Omanis and a major contributor to the Omani economy," Dr Al Rawahi says.&nbsp;<br>
                    <br>
                    As part of its plan, the ministry is in the process of establishing Oman's first fisheries training institute. Another innovative scheme is the recently launched Youth Vessel Project. This project provides a platform for young Omanis to not only be internationally educated in commercial fishing techniques, but also gives them the opportunity to own their own fishing businesses, including commercial vessels (three generations of vessel varying in size will be introduced). Currently the project's inaugural group of 32 keen young fishermen are in Tunisia receiving training. On their return to Oman, they will be able to jointly own a stake of between 60%-80% collectively in groups of four to eight in their own commercial fishing vessel. Financing for the young entrepreneurs will be provided by Oman Development Bank. A 'mother' company will hold the remaining 20%-40%. Al Rawahi says the ministry aims to put between 800 to 1,000 young Omanis through the scheme over the next few years.&nbsp;<br>
                    <br>
                    The idea is not only to provide rewarding jobs for school leavers in a growing industry, but also to allow Oman to realise significant extra revenue being lost to international companies. Currently, all of Oman's commercial fishing is dependent on foreign companies as Omani companies without adequate fishing fleets and other resources sub-contract or lease their permits to external organisations. Another concern is the price foreign firms pay Omani fishermen for their catch, says Al Rawahi. One example is tuna: Foreign companies pay local companies between $US120-$US200 per metric tonne for tuna, while on the international market these companies receive around $US4,000 per tonne. By developing an efficient local commercial fishing industry of international standards, Oman and its fishermen will no longer have to rely on foreign companies to access overseas markets and will be able to directly access, and therefore directly benefit from, international wholesale market rates.</font></p>
