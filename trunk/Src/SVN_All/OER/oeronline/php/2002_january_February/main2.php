                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Treasure Hunt<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>An antiquated mining code and tough environmental regulations have all but stifled the growth of Oman's mineral sector. But a new mining law, due to be unveiled this year, promises to make mineral exploration and development worth the bother for private investors</b></font><p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg16.jpg" align="left" hspace="5" vspace="5" width="225" height="150"></font><font face="Verdana" size="2">For a country blessed with bountiful mineral wealth - surpassing even that of its oil-rich partners on the Gulf Cooperation Council - Oman has little to show for it. The mineral sector's contribution to the gross domestic product (GDP) currently stands at a paltry 0.32 per cent, straggling behind even fledging sectors like travel and tourism.&nbsp;<br>
                    Mineral production generated RO33.32 million in revenues during 2000, climbing a modest 11 per cent over the previous year's earnings of RO30.08 million.&nbsp;<br>
                    The focus remained on traditionally mined deposits - marble, gypsum, limestone, industrial salt, chromites, gold, and building materials - with little evidence that other veritable hoards would be tapped anytime soon.&nbsp;<br>
                    Private sector interest in the mining industry continues to be tepid at best.&nbsp;<br>
                    Fresh in the minds of many potential investors are the many high-profile mining initiatives that came crashing to earth even before they got started. To compound matters, the continuing slump in international commodity markets is also doing little to instil confidence in the sector.<br>
                    The main sticking point is the outdated Petroleum and Minerals Law of 1974, which the private sector says is out of sync with the modern business climate. Furthermore, tough environmental criteria for mineral development, while necessary to safeguard Oman's fragile eco-systems, have unwittingly smothered many promising initiatives.&nbsp;<br>
                    But the Government has vowed to scrap present mining regulations and put in place "liberal and flexible" legislation as a first step towards creating a strong mining sector.&nbsp;<br>
                    "The new law will make it attractive for the private sector to invest in the mining industry in Oman," says Dr Hilal bin Mohammed Al Azri, Director General of Minerals at the Ministry of Commerce and Industry.&nbsp;<br>
                    "Since this sector is still in its infancy, the Government is fully aware that the private sector would need some assurance of a healthy return on its investments."<br>
                    Officials have hinted that mining incentives are likely to be far more generous than those being offered to the manufacturing sector.&nbsp;<br>
                    Unlike industrial ventures where returns are assured within the first two to three years of operation, mineral exploration and development can take at least five years before any payoff on the investment materialises.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/pg18.jpg" align="left" hspace="5" vspace="5" width="225" height="222">The challenge for the Government, therefore, is not only to make mining an attractive proposition for private investment, but also to successfully rope in experienced international firms.<br>
                    Industry watchers are predicting that the new mining law, like the groundbreaking power privatisation policy of the Government, will be a trendsetter of sorts.&nbsp;<br>
                    There are suggestions that private investors may be allowed 100 per cent project ownership of mines. Already 10 mines have been mapped out for potential development during the current 6th Five Year Plan (2001-2005).<br>
                    Dr Al Azri stresses that the Government's push for a robust mining industry will not be limited to merely harnessing the Sultanate's mineral deposits for export. The larger goal is to achieve maximum added value through refining and processing industries, and the manufacture of metal and non-metallic products.&nbsp;<br>
                    A vibrant mining sector has the potential to make a tangible contribution to the development and diversification of the Omani economy, says Dr Al Azri.&nbsp;<br>
                    Moreover, as most of the mines are located away from urban areas, their exploitation will contribute to economic growth and employment generation in less developed regions of the Sultanate.<br>
                    There is quiet optimism that local and international firms will eagerly line up for a stab at potentially rich pickings in the mining sector when the new law comes into force.&nbsp;<br>
                    Oman has large resources of industrial rocks and minerals, some of which are being exploited.&nbsp;<br>
                    They include silica sand, dolomite, limestone, gypsum, ornamental stone, clays, rockwool, iron oxides, heavy sands, wollastonite, guano, celestite, asbestos, aggregate, laterite and barite.&nbsp;<br>
                    Geological surveys have also uncovered potentially promising reserves of manganese, lead, iron, zinc, chrome, phosphates, gold, silver and nickel.&nbsp;<br>
                    Oman's vast mineral reserves include about one billion tonnes of gypsum, which is used in the manufacture of cement, ceramics, plaster and boards.&nbsp;<br>
                    Of particular interest is a large, commercially attractive deposit in Al Shuwaymiyah in the southern region, which is believed to hold 165 million tonnes of gypsum.&nbsp;<br>
                    Dolomite is another promising mineral found in Al Shuwaymiyah, part of a hoard of more than 500 million tonnes in deposits discovered in different locations in the country. Coal deposits, with reserves of over 122 million tonnes, have been discovered near Sur in the eastern region.&nbsp;<br>
                    But it is copper mining that is likely to herald the birth of a reinvigorated mining sector in the Sultanate.&nbsp;<br>
                    The discovery of huge reserves of sulphide (copper) ore near Yanqul in the Dhahirah region has raised hopes of a revival of the country's once-lucrative copper extraction and refining industry, which boasts a 5,000-year history.</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/pg19.jpg" align="right" hspace="5" vspace="5" width="192" height="162">A joint Omani-Japanese team of geologists is currently assessing the feasibility of developing the reserves, estimated to contain roughly 13 million tonnes of sulphide ore, laced with small quantities of gold ore as well.&nbsp;<br>
                    According to Yoshiaki Shibata, Japanese geologist on the team, a final report will be submitted to the Government in March.&nbsp;<br>
                    A green light on Yanqul's development will represent an enormous boost for the state-owned Oman Mining Company (OMCO), which currently imports copper ore concentrates to keep its Sohar smelter operational.&nbsp;<br>
                    A subsidiary of the Japanese industrial giant, Mitsubishi Corporation, is known to have voiced interest in a possible joint venture arrangement with OMCO to develop the mine, subject to its commercial viability being proven.<br>
                    Other minerals like high-grade silica sand and quartzite have also been the subject of strong international and local interest.&nbsp;<br>
                    Three years ago, a multinational consortium involving Russian, French, Norwegian and Omani firms unveiled ambitious plans to exploit these deposits for the manufacture of polysilicon ingots - the critical base material in the production of microchips and semi-conductors for the trillion-dollar information technology and electronic industries.&nbsp;<br>
                    The Government extended robust backing for the project, envisioning an investment of around US$600 million. Raw material for the project was to have been sourced from a quartz deposit located at Salil, one of five sites in the Sultanate where high-grade silica can be found. Although the project was eventually shelved, the availability of high-quality silica sand has continued to generate interest among prospective mining firms.<br>
                    Of late, however, the focus of the country's mineral potential has focused on the possible presence of platinum group metals in the Samail Ophiolite in southeast Oman - the largest mass of exposed, ancient oceanic crust in the world.&nbsp;<br>
                    The discovery in the deep ocean of active hot springs actually depositing metals such as copper, lead and zinc has revealed that ocean crusts have the potential to produce ore deposits of valuable metals.&nbsp;<br>
                    It is argued, therefore, that the Samail Ophiolite has the potential of containing large accessible deposits of valuable metals.<br>
                    At a key international conference on Oman's geology, hosted by Sultan Qaboos University last year, Commerce and Industry Minister Maqbool bin Ali Sultan told delegates that platinum metals had indeed been encountered in the Ophiolite.&nbsp;<br>
                    This crucial finding, he said, pointed to substantial treasures waiting to be found in Oman's subsurface.&nbsp;</font></p>
