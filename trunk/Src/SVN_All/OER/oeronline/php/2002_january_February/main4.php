                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The Scent of Success</font><br>
                    <br>
                    <font face="Verdana" size="1"><b>Ajit Kumar Hamlai has built the Ajay Group into one of the most successful corporates in Oman. He spoke to Shilpa Mathai about the evolution of his company, the core values that drive his approach to business, and the new ventures he has in the pipeline&nbsp;</b></font><p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg44.jpg" align="left" hspace="5" vspace="5" width="139" height="193"></font><font face="Verdana" size="2">An entrepreneur who stops to smell the roses, and is so intrigued by a bird's nest he copies the design to build himself a tree house... Ajit Kumar Hamlai, Chairman and Managing Director of the Ajay Group of companies, breaths this exuberance and zest for life into all his business endeavours - the reason he constantly succeeds in breaking new ground.&nbsp;<br>
                    "In business, I always maintain," he smiles charmingly, "one has to think creatively. Merely transplanting an idea or a model which has worked elsewhere or copying an existing venture will never work. You have to find your own place under the sun - find your own niche and then work hard to make sure no competitor steals a march over you."<br>
                    A player in this market since 1961, Hamlai has done just that.&nbsp;<br>
                    When in the 1980s he discovered a market existed for quality perfumes and oils to cater to Arabian sensibilities, he decided he had hit upon his niche. Despite his trading background - his family business involved importing and trading foodstuffs - Hamlai was undaunted.&nbsp;<br>
                    "I knew the time had come for us to diversify," he says. "Our trading operations were well entrenched, and it would only be a matter of time before the industrialisation process was triggered in Oman. I wanted a foothold in the manufacturing sector.&nbsp;<br>
                    "Though I wanted to promote agro-based industrialisation, I didn't want a "me-too" product. It had to be a novel concept that was also economically viable. Oman is an open market where free trade thrives; import duties are abysmally low so unless you had a product that was competitive in terms of pricing and quality, you would end up being a casualty. I tend to view this as a mixed blessing - an opportunity wrapped up as a challenge."&nbsp;<br>
                    Hamlai says perfumeries attracted him, but he had to think out of the box and be practical at the same time.&nbsp;<br>
                    "One has to be a dreamer, but you should also have your feet planted firmly on the ground and be aware of your limitations.&nbsp;<br>
                    "There was no way I could take on the might of the well established European brands. So instead of competing head on, I decided to go in for a product that was not on their agenda.&nbsp;<br>
                    "The odds against success were huge," he reminisces. "I didn't exactly have an open chequebook, but getting a project like this off the ground takes a lot more than money."<br>
                    He spent months in the jungles of Assam in India "getting a feel for Agarwood", his primary raw material. Oud oil extracted from Agarwood is a highly precious essential oil in the manufacture of classical perfumes. Chemical substitutes are no match for this natural oil, but extracting it from the wood is extremely expensive, which was the reason the Europeans stayed away.&nbsp;<br>
                    "But my reasoning was that with the right know how and technical expertise, why shouldn't I win the game?" Hamlai says. "There was virtually no competition, save some isolated companies in the region. As far as I was concerned it was a level playing field."&nbsp;<br>
                    Though he had done his homework - "I make sure I wet my feet first; I get a thorough grounding in any industry before I venture in" - he started off small. With a capital of RO5,000 and on basic semi-automated machinery, the company rolled out its first produce in 1986.&nbsp;<br>
                    "It was a winner," he grins. "The finished product exceeded all my expectations."&nbsp;<br>
                    Today the company is a major producer and exporter of essential oils and perfumes with an annual turnover that runs to tens of million of rials.<br>
                    "This reinforces my fundamental belief that if you offer people true value for their money and never compromise on quality, you have a winning product on your hands".<br>
                    Never content to rest on his laurels, Hamlai ploughed on.<br>
                    "We moved from perfumes to other allied products. I always opt for maximum utilisation of raw materials and try and add value to any by or end product. So for instance, we convert the wood pulp left after oil extraction into incense. From the first product, namely classical perfumes, I literally added 99 other spin offs to the kitty. Our product menu gets stretched all the time, a case of one thing leading to another."&nbsp;<br>
                    Today the group has diversified into a whole hoard of activities, with 16 companies operating in areas like agriculture, manufacturing of industrial products and cleaning agents, food processing, construction, retailing, real estate, engineering, pharmaceutical and alternate energy utilisation.&nbsp;<br>
                    "I have diversified into various ventures according to my circumstances and surroundings," he explains. "After we fine-tuned our perfumes, we moved into aerosol sprays. Though ten years ago CFC was a non-issue, I decided to be eco-friendly and opted for clean technology, which was 50 per cent more expensive."<br>
                    It is this vision and foresight that drives the company. Little wonder, then, that a rank newcomer to corporate Oman a decade ago is today a bustling enterprise bubbling with new ventures.&nbsp;<br>
                    A number of new projects are on the anvil. Construction is underway on an RO3 million naturopathy and alternate therapy hospital and resort in Barka, a themed restaurant "which features enthralling sonic and visual effects" is planned for Muscat, and an RO19 million 5-star sea resort is due to open in Al Haail in 2004.&nbsp;<br>
                    Doors open at the group's massive RO10 million Markaz al Bahja shopping complex near Seeb early this year.&nbsp;<br>
                    Hamlai is unfazed by any threat of competition from the neighbouring Muscat City Centre.<br>
                    "The hypermarket will offer completely different concepts from what's on at City Centre," he says. "We will give people entertainment and excitement that they do not get elsewhere; if you give people something unique, you don't have to worry about storm clouds like competition.<br>
                    "What you have to worry about though," he continues, "is your brand equity. You have to build your brand, which is never easy. It requires time and hard work. But once you establish your credentials, make sure every consumer gets his money's worth from your products, then you are on safe ground. I never compromised on my principles, I didn't change them for money. I carry on doing my business ethically and with sound values."<br>
                    Which is why Hamlai takes time to smell the roses on the way. At the end of the day, he values his job satisfaction. The pure pleasure he gets from a job well done, he says, is the best reward of all.&nbsp;</font></p>
