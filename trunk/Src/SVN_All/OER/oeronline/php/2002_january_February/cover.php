                  <td width="100%"><img border="0" src="../../images/2002_january_February/Pg20.jpg" align="left" hspace="5" vspace="5" width="225" height="173"><font face="Verdana" size="4" color="#FF0000"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
                    </font>Mind the Gap</font>
                    <p><b><font face="Verdana" size="1">Oman might have joined the World Trade Organisation, but are businesses in the Sultanate ready to compete in a deregulated market? Experts say Omani firms are leaving themselves dangerously vulnerable to emerging competition if they fail to analyse their markets and address their own weaknesses.&nbsp;<br>
                    Matthew Brockett reports on the information gap</font></b></p>
                    <p><font face="Verdana" size="2">Whenever Dubai-based businessman Sam Rao visited Muscat, he always stayed in the same hotel. And he was a good customer. A frequent visitor to the Sultanate, Rao estimates he stays at least 80 nights a year in Muscat. In addition, his software company, Oracle, makes regular use of the conference facilities at the hotel he stays in for seminars and other business events.<br>
                    So when Rao became unhappy with his regular hotel's service and decided to take his business elsewhere, he was surprised he didn't hear from management.&nbsp;<br>
                    "There was complete silence," he says. "Then about eight months later, for some reason they gave me a call saying 'we would like to meet with you, we would like your business'.<br>
                    "I was pretty sure the guy who was calling me only knew there was a company called Oracle, but he didn't really know me as a customer. He didn't know what my issues were."<br>
                    This failure to understand the customer's needs, says Rao, is a common characteristic of businesses in Oman and, indeed, throughout the Gulf. Worse still is the lack of research and analysis by companies of their market or customer base.&nbsp;<br>
                    In other words, information is not being gathered or used effectively, creating a gap through which customers can and do fall.<br>
                    Rao says a lack of competition has allowed companies to get away with a degree of complacency in the past. But as the Gulf States increasingly open their economies to the global marketplace, he believes the companies that do not change their ways will die.<br>
                    "Competitiveness is not a major issue right now, because we still enjoy a great degree of protection through regulation," he says.<br>
                    "But what happens if the barriers do actually come down, and if regulation gives way to deregulation? How competitive are we?<br>
                    "In our view, not a lot of organisations actually have a great understanding of their customer base. What they have is a list of customers, a list of products or services they sell, and it's really not a very sophisticated way of doing business."&nbsp;<br>
                    <br>
                    </font><b><font face="Verdana" size="1">KEEP PACE WITH CHANGE</font><font face="Verdana" size="2">&nbsp;</font></b><font face="Verdana" size="2"><br>
                    <br>
                    Market researchers say many Arab companies are failing to keep pace with the changes in Arab consumers. Whereas once one product suited all, today consumers are far more demanding.<br>
                    "Arab consumers across the GCC used to dress the same way, buy the same brands and drive the same kind of cars," says Dubai-based Ken McDermott, director of special projects at NFO Merac.&nbsp;<br>
                    "However, the advent of satellite television has changed all that. It has given them a window on the world and exposed them to foreign cultures. They are fascinated by some of the lifestyle freedoms available in the West and this has sometimes caused a radical reappraisal of their own attitudes and values."&nbsp;<br>
                    Sanjay Kumar, former retail brand communications manager for Shell Oman Marketing Co, says the days of one-size-fits-all are over in the Middle East.<br>
                    "Customer needs are now so specific and customer groups are so segmented, you have to have specific offers for each segment," he says.&nbsp;<br>
                    "This means we need a value proposition for each customer segment that we define. The archaic idea about the brand just representing a logo is over. Today it's about getting closer to the customer, getting personal with the customer."&nbsp;<br>
                    <br>
                    </font><b><font face="Verdana" size="1">SIZE UP THE MARKET&nbsp;</font></b><font face="Verdana" size="2"><br>
                    <br>
                    The size of the Omani marketplace is clearly an issue. Suhail Mustafa Khan, manager of Simpa Marketing Research and Consultancy in Muscat, says that because international companies see Oman as such as small market, they are failing to appreciate, or simply ignoring, its nuances.<br>
                    "They treat this country as being similar to the UAE or Saudi Arabia," he says.&nbsp;<br>
                    "But it's not. This country has got a totally different character. If you look at the people, they're very mild; not as conservative as Saudis but not as modern as the Emiraties. They still value family, they have a lot of traditions, but they're different in their taste, in their thinking.&nbsp;<br>
                    <img border="0" src="../../images/2002_january_February/Pg22.jpg" align="left" hspace="5" vspace="5" width="150" height="189">"And here you will find a lot of different communities also. The Lawatias (Iranian descent) have got a different outlook on life, the Zanzibaries have a different outlook of life. Then there are the Dhofaries from Salalah, the Balushies, and the local Omanis. You find they've all got different outlooks.<br>
                    "I have seen certain communities have a liking for a certain brand or a certain type of car. But there is no positioning being done. It's all uni-marketing, everyone is marketing to the same crowd."<br>
                    Kumar says it is the lack of competition that causes big companies to treat Oman as one market. But he believes this is about to change.<br>
                    "There is obviously an attitude that marketers are not so keen in fulfilling consumer aspirations because of the fact that competition has been limited," he says.&nbsp;<br>
                    <br>
 "But that is going to change, and it's going to change very fast. I think it's already beginning. Retailing is an area where we're seeing a lot of activity, IT is an area where we've seen some activity.<br>
                    "So there are sectors that are facing pressure already."&nbsp;<br>
                    <br>
                    <br>
                    </font><b><font face="Verdana" size="1">RESEARCH, ANALYSE, PLAN AHEAD</font><font face="Verdana" size="2">&nbsp;</font></b><font face="Verdana" size="2"><br>
                    <br>
                    Khan says companies in Oman need to analyse their customer base more thoroughly to identify its varying needs.<br>
                    "A lot of business is being missed," he says. "There's no strategic thinking in the companies, they are very short-term in their thinking."<br>
                    He believes market research is under-utilised as a business tool in Oman, and says firms will need to use it more if they are going to compete successfully in the longer term.&nbsp;<br>
                    He says that at present, many managers operate on their "gut feeling" rather than proper analysis of their customer base and market.&nbsp;<br>
                    "People think they know everything, but we have come across findings that are totally contrary to what managers feel," he says.<br>
                    "The strategic importance of market research is not really understood here."<br>
                    Companies in Oman also need to recognise the importance of planning beyond next year, he says.<br>
                    "I don't find companies planning five years ahead. They just want to see today. But five years from today, where am I in my customer's mind? Companies look at last year's sales and say 'where will I be next year?' There has to be a shift in that thinking."&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>THE EXPATRIATE SYNDROME&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Part of the problem, Khan says, is that many managers in Oman are on short-term contracts. Rather than invest in market research, they spend their money on short-term marketing tools, like prizes and promotional competitions.<br>
                    "Most managers are expatriates, they are here for a short time, maybe about two years," he says.<br>
                    "The manager, when he comes here, doesn't know whether he's going to last for the next five years or not. It takes time for research to pay off, and he doesn't have that kind of time. So the money he's got at his disposal, the marketing budget, goes into the promotions so he can say to his boss: 'look, I've devised a new promotion'."<br>
                    Figures compiled by Simpa show that price promotions, where firms try to lure customers with prizes and money, dominate advertising in Oman.<br>
                    Of the RO8.5 million spent on advertising in Oman in the 11 months to December 2001, RO4.2 million was spent on promotions. Significantly, only RO2.2 million was spent on brand building.&nbsp;<br>
                    "I am a strong advocate of brand building," says Khan.<br>
                    "I think it's the right time to spend more on brand building because there is no clarity in the consumer's mind. There are so many brands in the market, there are so many products, how do you differentiate yourself from the others? Companies have to start investing in brands.<br>
                    "A little bit of branding pushes the brand into the consumer's mind, yet companies are not considering it."&nbsp;<br>
                    <br>
                    </font><b><font face="Verdana" size="1">RESISTANCE TO CHANGE&nbsp;</font></b><font face="Verdana" size="2"><br>
                    <br>
                    Many companies in Oman are resistant to change. Khan says a big issue is that middle managers tend to put their heads in the sand rather than draw attention to a problem and try to fix it.<br>
                    "They know it, but they don't want to tell anybody about it," he says.<br>
                    "They don't want to tell the management 'look, there is something bad happening', because then they will be blamed for it. But what have they done to stem the rot?"<br>
                    He believes there must be a mentality shift if this problem is to be solved.<br>
                    "The willpower has to come from the management," Khan says.<br>
                    "If the management does not want to know their weaknesses, then there is a cancer in the company that spreads."<br>
                    Kumar says smart companies, even successful ones, will change now rather than wait for competitive pressures to emerge. He holds up Shell Oman Marketing Co as an example, saying that despite being dominant in its market it still engages in customer research and analysis to define key consumption segments.<br>
                    Khan says his biggest achievement in Oman to date is to convince laundry company Snow White to undertake market research.&nbsp;<br>
                    He says that despite being the biggest laundry company in the Sultanate, Snow White recognised it could still improve its business by addressing its weaknesses.&nbsp;<br>
                    As a result of the research, Snow White changed its trading hours to capture more clients, and was able to reduce delivery times.&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>SEPTEMBER 11&nbsp;<br>
                    </b></font><font face="Verdana" size="2"><br>
                    The September 11 terrorist attacks on the United States have had an enormous impact on consumers and businesses around the world.&nbsp;<br>
                    Kaare Johansen, regional director of marketing for Six Continents Hotels at the Al Bustan Palace, says the ability to respond and adapt rapidly to changed circumstances can be the difference between success and failure for any business.&nbsp;<br>
                    <img border="0" src="../../images/2002_january_February/Pg25.jpg" align="left" hspace="5" vspace="5" width="201" height="287">He confirms that hotels in Oman have been affected by the September 11 crisis, with many travel-wary overseas guests and companies cancelling their holidays or conference bookings. This has necessitated a complete and rapid change in hotel marketing strategies.<br>
                    His response has been to focus on regional tourism, rather than international, and to expand rather than retrench the hotel's marketing operation. "We met on September 22 and started strategising," he says. "Since then we've gone full blast. We haven't reduced our marketing budget, in fact we've increased staff in sales.&nbsp;<br>
                    <br>
 "This is crunch time."<br>
                    Johansen says though some companies in Oman might be slow to adapt to change, he believes government agencies are very aware of the challenges ahead.&nbsp;<br>
                    He cites the Directorate-General of Tourism and the Omani Centre for Investment Promotion and Export Development (OCIPED) as two organisations with a proactive approach to making Oman competitive on the global stage.&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>BE PROACTIVE&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Oracle's Sam Rao says one area where companies will have to improve in order to compete is customer service, and key to this will be the more effective use of information.&nbsp;<br>
                    "In order for us to be sophisticated in our ability to understand customers, in our ability to respond to customer inquiries or customer complaints or service requests, we need to have the information right there," he says.&nbsp;<br>
                    "We don't have the luxury of waiting for a day or two, because customers are impatient. The more sophisticated they become, the more aware they become, the less patience they have in dealing with us."&nbsp;<br>
                    Oracle sells software packages that allow businesses, like the hotel Rao deserted in Muscat, to easily analyse their customer base and recognise trends.<br>
                    He says his old hotel could have retained him as a customer if it had put the right systems in place.&nbsp;<br>
                    "If they were smart enough, they could have had a system that did certain things," he says.&nbsp;<br>
                    "When they captured me as a customer, they could easily have determined the likely frequency and intervals of my stays. If I haven't checked in after a certain amount of time, the computer system should trigger an alert, an email alert, or simply a note to say, 'hey, this guy hasn't stayed here for so long, and based on our research, our initial talk with him, we expected him to do xyz. These things haven't happened, why don't you check?'<br>
                    "It becomes proactive marketing," says Rao. "You call me, say 'hey what's happening, have you moved?' You don't wait six months to do that."<br>
                    Unfortunately, many businesses in Oman still do.&nbsp;</font></p>
