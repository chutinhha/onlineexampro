                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Omantel to unveil new Internet
                    Services<br>
                    <br>
                    </font><b><font face="Verdana" size="1">Web Usage Booms in the Sultanate<br>
                    </font></b><font face="Verdana" size="4" color="#FF0000"><br>
                    </font><img border="0" src="../../images/2002_january_February/pg12.jpg" align="left" hspace="5" vspace="5" width="225" height="191"><font face="Verdana" size="2">Omantel is set to unveil a suite of new Internet services as Internet usage in the Sultanate booms. Internet subscriber growth has more than doubled in Oman since tariffs were reduced one year ago, says Omantel General Manager Internet Services, Ali Abdul Amir Al-Abduwani.<br>
                    He told OER the reduction in Omantel's Internet tariff to a flat rate of 180 baisas per hour has had a dramatic impact on the number of people getting connected.<br>
                    "Before then it used to be around 800 to 900 new subscribers per month; now it is more than 2000 new subscribers per month."<br>
                    Today there are more than 45,000 subscribers in Oman, up from 28,000 at the end of February last year.<br>
                    The sharp rise in Internet subscribers, as well as the opportunities made available by new technology, has prompted Omantel to invest in a range of new online services.<br>
                    Soon Internet users will be able to shop, bank, and pay their bills online. Businesses and government agencies will be able to hold video conferencing and interact with each other and their customers over the Internet.<br>
                    "For e-commerce we are now in the final stage of negotiations to implement a payment gateway," says Al-Abduwani.&nbsp;<br>
                    "Companies and the Government will be able to use the payment gateway, then you can have business-to business and business-to-consumer transactions over the Internet."<br>
                    Omantel is also looking at the possibility of offering videos on demand to home users over the Net, though regulatory approvals are yet to be taken.<br>
                    The new services are possible because Omantel has dramatically increased the bandwidth capacity of its "backbone" connection to the worldwide web, enhancing the volume of data that can be downloaded from overseas websites and the speed of access.<br>
                    Now it is installing ADSL technology that will do the same thing to the connection between Omantel and its subscribers. ADSL is a high-speed data line that will allow consumers to download vastly more data from the Web much more quickly than they can at present.&nbsp;<br>
                    "ADSL services will provide the customer with a very high bandwidth," says Al-Abduwani. "It is high-speed data line. When you have ADSL service you can watch videos online. Just click, and you can go anywhere, shopping, banking, entertainment, everything."<br>
                    ADSL should be available by the end of the first quarter of this year.<br>
                    Using the Internet is also getting easier. Omantel has already introduced a real-time online billing system for Internet subscribers and GSM and fixed-line customers. The system enables users to view their account and their usage up to the last disconnection.&nbsp;<br>
                    Usage charges and other Omantel services are also available on the site. (www.omantel.net.om)<br>
                    Al-Adbuwani says the service has allowed Omantel to provide a lot more information to its subscribers, which is cutting the incidence of Internet fraud.<br>
                    "Suppose I want to know if somebody else is using my account, I can find out online," he says. "It shows you the usage, the telephone number from where you dial, everything. It is reducing fraud, because everybody is aware now from where people are calling."<br>
                    Since the new billing system was introduced, Omantel has also been able to collect a lot more information about its Internet subscribers. For example, subscribers spend an average of 30 hours a month on the Internet - roughly one hour a day.<br>
                    Most subscribers - 91 per cent - are residential users, with businesses accounting for 8 per cent. The majority of subscribers (43 per cent) are aged between 21 and 35; 34 per cent are aged 31 to 40; 12 per cent are 41 to 50; and 6 per cent are under 20 years old.</font>
                    <p align="center"><img border="0" src="../../images/2002_january_February/Graph1.jpg" width="400" height="302"></p>
                    <p align="center"><img border="0" src="../../images/2002_january_February/graph2.jpg" width="400" height="302"></p>
                    <p align="center"><img border="0" src="../../images/2002_january_February/graph3.jpg" width="400" height="301"></p>
                    <p align="center"><font face="Verdana" size="1">Source: Omantel</font></p>
                    <p><font face="Verdana" size="2">Fifty-five per cent of all subscribers are based in Muscat, with 14 per cent in the Al Batinah region, 9 per cent in Dhofar and 9 per cent in Al Sharqiyah.<br>
                    Also soon to be introduced are free "starter kits" with each new subscription that will help people to navigate and use the Internet. The kit will include a CD containing the latest browser, customised data, an Arabic web-page translator, and an Internet tutorial in English and Arabic.&nbsp;<br>
                    Al-Adbuwani says gaining access to a PC is often a big hurdle for would-be Internet subscribers, so Omantel will this year launch a package deal that includes a new PC with a subscription. Customers will be able to pay off the PC over a two-year period in small monthly instalments.<br>
                    He believes the package will enhance subscriber growth. "Some people cannot afford PCs, but when you give a PC as a package to them, for sure they are going to use it."<br>
                    Al-Abduwani says other Internet innovations to look forward to include web-hosting services, which will allow users to create and store their own home page in Oman rather than overseas, increasing speed and ease of access.&nbsp;<br>
                    Internet nodes are also being installed in other regions in Oman, so that people outside Muscat will not need to connect to the web via the node in the capital, again increasing speed of access.<br>
                    Looking further ahead, Al-Abduwani believes a move to content-based charging is inevitable as the world embraces third-generation technology. This will mean that rather than dialling a connection every time you want to browse the Internet, users will be connected 24-hours a day.<br>
                    "Now mostly we are charging for usage, but if you look at the US and Europe, the charging is mostly content-based," he says.&nbsp;<br>
                    "We are going towards that for sure. Maybe there will be a fixed, flat rate for subscription to the Internet, then you will be charged on the number of hits per site or bandwidth utilisation, because you will be connected to the network 24 hours."<br>
                    The Internet, he says, is changing the way we work and live.&nbsp;</font></p>
