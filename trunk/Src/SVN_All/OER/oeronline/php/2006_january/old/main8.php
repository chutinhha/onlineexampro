<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b>
            <font face="Verdana" size="4" color="#FF0000">You’re in the Wrong 
Business...Now What?<br>
            <br>
            </font></b><font face="Verdana" size="3"><b>
<img border="1" src="managment1.jpg" width="131" height="160" align="right" hspace="5" vspace="2">Y</b></font><font face="Verdana" size="1">ou 
feel stuck. The product offering that put your organization on the map has gone 
stagnant. Your gut tells you that your customers are moving in a different 
direction. But changing your core business model feels impossible, if not risky.
<br>
<br>
If such a scenario describes your organization, you are not alone. It is not 
just the business enterprise that is getting lost in transition; a large slice 
of not-for-profit organizations also sense they are waking up in the wrong era. 
The rapid pace of change we are experiencing leaves no organization untouched. 
The cause for our condition cannot be narrowed down to any single factor. I 
prefer to describe it as the perfect storm of significant economic, 
technological and cultural shifts.<br>
<br>
The music industry feels the crisis in dramatic fashion. The CD has 18 months to 
live - at least that was the diagnosis of the Wall Street Journal in a recent 
feature story. My friend T-Bone Burnett, the stellar music artist and producer, 
tells me that in a couple of years the vinyl album will outsell the CD. That’s 
not because vinyl will make a huge comeback. To the contrary, CDs will tumble. 
Vinyl at least has a rarified customer base that craves for its distinct sound.<br>
<br>
Imagine making your livelihood in the music industry. The CD is your 
bread-and-butter inventory item. Yet, you know you have a terminal illness and 
your business model must undergo a radical therapy. The path forward is not 
clear. Where do you turn next?<br>
<br>
In some respects, the music industry may be lucky to know its future demise. The 
Next Reality is murkier compared to other sectors. And with heavy fog impairing 
its vision, the organization is likely to err on the side of the status quo.<br>
<br>
<img border="0" src="managment.jpg" width="250" height="160" align="left" hspace="5" vspace="2">Most 
managers, frankly, will cling to their present operational model even if it 
requires a good dose of denial to do so. They have invested resources to sustain 
the present infrastructure and feel as if there is no turning back. In other 
words, they have to get a return that will justify the past investment. <br>
<br>
In other cases, personnel and material resources are so geared to keeping the 
train running down the current track that it is almost unthinkable to consider a 
different course. Or it could be that a founder launched the enterprise and 
cannot imagine running it with any other configuration.<br>
<br>
One of my favorite Peter Drucker stories places him in a room with Jack Welch 
not long before Welch assumed the CEO post at General Electric. Welch reports 
that Drucker asked him two simple questions that rocked his world. “If you 
weren’t already in a business, would you enter it today?” Drucker led with a 
deft right. “And if the answer is no, what are you going to do about it?” he 
added with the left hook.<br>
<br>
You know the rest of the story. Welch determined that a GE business unit must 
stand at No. 1 or No. 2 in its class. If not, he ordered the unit to be fixed, 
sold or closed.<br>
<br>
It was Drucker’s brilliance to cut to the chase. The first step in a 
self-assessment process is to re-evaluate your foundational assumptions. If you 
were to start your organization today, would you feature the same products or 
services that currently stand at the core of your enterprise? Or could it be 
that a different product or service would better address the needs of your 
constituency and fulfill the mission of your enterprise?<br>
<br>
If the legacy model is no longer mission-critical, it will deliver diminishing 
returns. Meanwhile, other organizations that are not bound to that legacy will 
emerge to engage your constituency in a more direct and relevant way. Even if 
you are a leader in your niche, you cannot afford to drain your resources in the 
wrong products while simultaneously building new product lines. A more focused 
competitor will surpass you in due course.<br>
<br>
Leadership, today, requires the wisdom to discern when a legacy should be 
buried, and the courage to give painful birth to new organizational life.<br>
<br>
(David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States)</font></p>

