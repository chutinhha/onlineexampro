</font><b>
            <font face="Verdana" size="4" color="#FF0000">December blues, again!<br>
            <br>
            </font></b><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
market saw a new six-month low of 4722 in December and closed at 4845 on 22 
December 2005 as against the close of 4886 on 24 November 2005. There is a very 
high probability of a trend-reversal taking place any time. <br>
<br>
If we analyse its history, we find that the MSM index made important bottoms 
during the month of December in most of the years. In 2004, after touching a 
high of 3487 in June 2004, the market touched a bottom of 3302 in December 2004 
and after that it rose to a historic high of 5699 in June 2005. Similarly, in 
2001, the market touched a five-year low of 1461 in December that year before 
starting the current bull run. Hence, we will have to wait and see whether MSM 
history repeats itself.<br>
<br>
All the sector indices depicted similar performance during the month. The 
banking index touched a low of 6604 and closed at 6799 on 22 December. The 
industry sector index closed at 3753, lower than the close of 3933 on 24 
November. The services sector index closed at 1971, lower than the close of 1998 
on 24 November 2005.<br>
<br>
<b>MSM attractive</b><br>
Based on any parameter, the MSM is quite attractive when compared to other GCC 
markets. Its valuations are the lowest among other GCC counterparts. The market 
PE for MSM is 12 with Omantel and 10 without Omantel. The market PE for Qatar, 
Saudi and the UAE are above 40 levels, Kuwait 17 and Bahrain 10. Hence, Oman and 
Bahrain are enjoying the lowest valuations. However, Oman enjoys other distinct 
advantages compared to Bahrain, thanks to its huge land area, a widespread 
market and a significantly larger population, thereby creating greater market 
potential.<br>
<br>
Corporate performance in MSM has improved quite dramatically in the third 
quarter 2005. Out of the top 50 traded companies, 36 came out with improved 
results on a YoY basis. All the four listed banks (BankMuscat, Oman 
International Bank, National Bank of Oman, and Bank Dhofar) came out with 40-160 
per cent improvement in profits compared to last year. The average dividend 
yield of top 10 companies is 5 to 6 per cent compared to the GCC average of 2 to 
3 per cent. <br>
<br>
<b>Riveting fundamentals</b><br>
On the macro economic front also, the Omani economy is doing exceedingly well. 
The nominal GDP growth in 2004 is 14 per cent, which is a 100 per cent increase 
over the previous year. The Government is launching a host of new investment 
projects in infrastructure and tourism. The budgetary position is comfortable, 
with a fiscal surplus of 2.4 per cent of GDP in 2004. Prices are under control 
and the Whole Sale Price Index (WPI) is less than the 1995 level. The position 
on external front is comfortable, with a current account surplus of RO170 
million in 2004. <br>
<br>
<b>Projections</b><br>
With all these feel-good factors in place, the market is expected to accelerate 
in the next 3-6 months. In all probabilities, the MSM index may catch up with 
the previous peak of 5700 levels. The investors are advised to stay invested in 
good shares to make decent gains in the medium term.</font></p>
