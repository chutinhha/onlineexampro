<p align="justify" dir="ltr">
<font face="Verdana" size="1"><b>Jack Canfield, the renowned ‘life coach’ and 
author of best-seller Chicken Soup for the Soul, in an exclusive tete-a-tete 
with Akshay Bhatnagar, talks about the principles that can make a person 
successful in his professional and personal life</b><br>
<br>
<b>
<img border="1" src="face1.jpg" width="150" height="140" align="left" hspace="5" vspace="2">Why 
do many professionally successful people fail to replicate the success in their 
personal lives?</b><br>
The formulae for success could be applied in the same manner in both 
professional and personal lives. People are much more effective at work because 
they have to be successful. Otherwise they will lose their jobs. The big mistake 
they do is that they don’t transfer the same principles they are using at work 
over to their personal lives. In other words, you have goals and objectives in 
your professional life, but you don’t set goals in your personal life. For 
example, many people have no set goals for their relationship with their wife or 
family. If one has to be closer to his spouse, there has to be a vision on how 
to go about it. <br>
<br>
In our jobs, to keep the boss happy, we have goals that are measurable and 
expected to be achieved within a certain time frame. Some people say they want 
to get along better with their kids but how do you measure that and what’s the 
timeframe for that? If one says I want to take each of my kids along for a 
30-minute walk weekly and talk about their life, that’s a definite plan, which 
is measurable. Or it could be that we are going to have a family night out once 
in fortnight where we spend time with each other and enjoy. <br>
<br>
Another critical factor is that many of us constantly work towards developing 
our work-related skills but we don’t develop the skills to manage our personal 
lives. <br>
<br>
Many of us read books to better manage our work but very few read books on how 
to manage the family in a better manner. <br>
<br>
<b>Do companies approach you to guide their employees on how to be successful in 
their personal lives also, besides the professional front?</b><br>
Generally, companies do ask me to help their employees to know about the success 
principles applicable to their professional as well as personal lives. When I go 
to a company and tell its employees that what I’m going to teach you is going to 
make you successful at home also, they are more excited. They feel that they are 
getting a double workshop. If I just tell them how they are going to be more 
valuable for their employers, they feel that they are being used. <br>
<br>
<b>Can you narrate some of your experiences, which have changed the personal 
lives of the people?</b><br>
I’ll give you two examples. Sometime back a gentleman who was into public 
speaking attended my seven-day long workshop on learning success principles. He 
said, “After attending your workshop, I have become a millionaire in two years. 
I have tripled my public speaking business. More than that, my children say they 
have got a new father. They say I have changed after attending the workshop. 
They say that I now listen to them and don’t lecture them. I now understand that 
their needs are different. I don’t give them everything that they want but I am 
more understanding now.” <br>
<br>
A few years back, in one of my workshops, I had a person who was working as a 
sales trainer with a large corporation and was earning $140,000 a year. He was 
stuck in that position for quite long and didn’t have a clue as to why he had 
not been able to progress further in his life. We did an exercise with him to 
become more successful in life. A year later, he was making $750,000 a year by 
working for just three days a week! <br>
<br>
<b>How should the top management handle the fear of failure?</b><br>
Everyone wants to perform his/her job well whether it is to please the 
shareholders or their superiors or whosoever they work for. The fear of failure 
makes the mind shutdown. There are two ways to handle fear. One is to feel the 
fear and do it anyway. That’s what most people do. <br>
<br>
The more effective way is to visualize yourself being successful. Fear is always 
an image of failure in the future. If I am the CEO of a company and I’m afraid 
and visualizing that the company’s stock might be dropping or business going 
down, that’s all in the future. But I am experiencing the fear in the present 
time by imagining these terrible things. All fear is self-created in the mind. 
Stop imagining negative developments. <br>
<br>
<b>You said that a person should know his unique proposition and build on it to 
become more successful. But how would a person know what his unique points are?</b><br>
Every person has something unique about him/her. To know about your uniqueness, 
ask other people. They will tell you what makes you different from others. You 
might get responses like you are a good organizer or you are good in solving 
problems or you are good in resolving conflicts among people. It’s not a very 
difficult exercise and it could be carried out with minimal effort to make you 
understand what makes you stand out in a crowd. Once I know what is unique about 
me, I can make efforts to use and further develop it. Do things that you love to 
do. And you will be surprised to know that people will pay you for doing things 
that you want to do. <br>
<br>
<b>Do you think that your success principles are universally effective?</b><br>
Yes, I do. I have taught in more than 24 countries and I find that everyone has 
similar needs and desires. All individuals want to be more effective in 
communication, want to perform well in life and get respected and appreciated by 
others. Every culture has achieved perfection in one way or another. For 
example, India has perfected the art of meditation way beyond other cultures. 
The western world is learning from India on this. <br>
But the western world has perfected business and economics better than many 
others. So we are all learning from each other to become better than what we 
are.<br>
<b><br>
What is the concept of a life coach? Do you think every individual should have a 
life coach to increase the probability of becoming successful in life?</b><br>
Take the example of sports. Most of the sportspersons have a coach to learn new 
ways to improve their performance. Similar formula applies in life also. A life 
coach apprises you of your strengths. He guides you on how you can play to your 
strengths to become more successful instead of groping in the dark. <br>
It is not necessary for everyone to have a life coach to become successful. 
There have been many examples of highly successful people who didn’t have a life 
coach to guide them but they still made it. But I believe that if you want to 
accelerate the rate of your success, one should have a life coach. I firmly 
believe in the role played by coaches and mentors in making us more successful. 
I myself have three-four coaches to guide me in the different areas of my life. 
And I pay them good money, as they are very effective!<br>
<br>
<b>Many people believe that the urge and desire to succeed comes from within a 
person and the other person cannot drive you towards it. Do you agree?</b><br>
I do agree that the desire to succeed comes from within. But there are lots of 
things that we want to do but these feelings get suppressed due to certain 
external factors like pressure from parents or teachers or office superiors to 
perform things the way they want us to do. The other person, people like me, 
acts like a mirror and shows them what they really are and inspire them to do 
what they are capable of doing. </font></p>
