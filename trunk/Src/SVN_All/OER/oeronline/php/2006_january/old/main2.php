    <p style="margin: 10px" align="center"><b><font face="Verdana" size="5">10 
    STEPS TO SUCCESS</font></b></p>
    <p style="margin: 10px" align="justify"><font face="Verdana" size="1"><b>1 
    Decide what you want in life </b>– Many people don’t do this. There are 
    seven areas in one’s life - finance, career, health, relationships, fun time 
    and recreations, personal things which one wants to do and contributions to 
    the society. When people look at these areas, they become clear on what they 
    want in their lives.<br>
    <br>
    <b>2 Know what it takes to become what you want to be</b> – I recommend that 
    we need to find people who have already done what we want to do. One needs 
    to ask them what we need to do in order to do what they have done. For 
    example, when we published Chicken Soup for Soul, we went to the top five 
    best-selling authors and asked them what we needed to do to achieve what 
    they had done in terms of selling millions of copies of their books. Besides 
    other things, they said that do at least one radio interview a day for a 
    year. We did that and Chicken Soup for Soul sold millions of copies. <br>
    <br>
    <b>3 Use the power of visualization</b> – Close your eyes every day for at 
    least 5-6 minutes and visualize those goals as complete. This programmes the 
    subconscious mind to come up with solutions. The subconscious mind is more 
    powerful than the conscious mind. The creativity comes out of discipline and 
    helps in focusing on your intended target. <br>
    <br>
    <b>4 Act to accomplish your goals</b> – Most people don’t act on their 
    ideas. They get great ideas but they are afraid of rejection. They are 
    afraid of looking foolish. They feel that they lack knowledge, power, money 
    and networking etc. to translate their ideas into reality. So taking action 
    is critical. <br>
    <br>
    <b>5 Feel the fear and do it anyway</b> – One should just simply act. I 
    believe in the law of probability. The more you try, the more are your 
    chances of achieving your goal. There are managers who are afraid of 
    disciplining their employees. They are afraid of speaking up and disagreeing 
    with their leaders. All these fears stop them from becoming successful 
    managers. Positive thinking generates positive frame of mind in others also 
    which helps in achieving corporate goals. <br>
    <br>
    <b>6 Perseverance</b> - Stay motivated! Most people give up too soon. One 
    shouldn’t give up. Take my example. My book, Chicken Soup for Soul, was 
    rejected by 144 publishers. Our agent finally gave up. We went to a book 
    exhibition where 4,000 publishers were participating. We contacted them one 
    by one. Few hundred more rejections came our way. One publisher finally 
    agreed to publish it. They were expecting to sell 20,000 copies but we said 
    we expected to sell one-and-a-half million copies in a year. They laughed at 
    us. But what followed is history. Follow the rule of ‘five’. Do five things 
    a day which takes you forward towards your goal. Never give up. <br>
    <br>
    <b>7 Celebrate your success</b> – One should celebrate one’s success and 
    reward oneself. Get the child in you come out and take steps to fulfill your 
    desires. <br>
    <br>
    <b>8 Positive self-talk </b>– More than 50,000 thoughts go through a 
    person’s mind in a day. Unfortunately, for majority of the people, they are 
    negative. Thoughts like ‘I can’t do this’, ‘I’m going to fail…’ are negative 
    and they affect the way we are perceived even before we talk. Talk 
    confidently and positively and people will respect you.<br>
    <br>
    <b>9 Mastermind alliance</b> – For a long-term success, we must have 
    mastermind alliance. This means 6-7 people or more (could be less also) 
    meeting regularly, say once in a week or in a month. This committed group 
    should comprise of successful people and has an objective to support each 
    other in becoming more successful. As some group members become more 
    successful, it builds confidence in other members to strive towards becoming 
    more successful. <br>
    <br>
    <b>10 Practise uncommon appreciation </b>– Believe it or not, employees are 
    more motivated by appreciation and an understanding attitude than they are 
    by good wages, job security and promotion opportunities.</font></td>
