Pvt. sector powering Chinese dragon</font><font face="Verdana" size="1"><br>
            <br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="1" src="sm_mun1.jpg" width="106" height="122" align="left" hspace="5" vspace="2">C</b></font><font face="Verdana" size="1">hina 
is the fourth largest country in terms of size and the largest in terms of 
population. Driven by domestic demand and supported by World Trade Organization 
accession, China's economy is expected to continue its robust growth over the 
next two years. The total value of goods and services has been growing at a 
double-digit rate in the last 20 years. Although growth figures are now in the 
single digits - about 7.3 per cent. As per the World Bank estimates, China's 
economy will account for 25 per cent of the total world economy by the year 
2025. Nottingham Business School's Professor Kulwant Pawar, a presenter at 
PEIE's forthcoming Smart Manufacturing Conference slated on 23–25 January 2006 
at Muscat Inter-Continental Hotel, talks on manufacturing and China <br>
<br>
<b>Talking of China, one often encounters the terms - Economic and Technological 
Development Zones (ETDZs), High-tech Industrial Development Zones (HIDZs), Free 
Trade Zones (FTZs) and Export Processing Zones (EPZs). Can you explain what each 
of these terms signify, how do they differ and can affect a company's selection 
of a location?</b><br>
Economic and Technological Development Zone (ETDZ) has a more general function, 
while High-tech Industrial Development Zone (HIDZ) focuses on high-tech 
industry. But in reality, the distinction between the two is not clear and often 
they are differentiated only by name. To date, there are 54 national-level 
ETDZs. The ETDZs have been quite successful in attracting foreign investment and 
have made significant contribution to export. On the other hand, Free Trade 
Zones (FTZs) and Export Processing Zones (EPZs) are kinds of special zones, 
whose main function is for international trading, exhibition and export-oriented 
manufacturing. Therefore, goods imported to these zones enjoy zero custom 
duties.<br>
<br>
<b>Today, over two-thirds of foreign manufacturing in China is for domestic 
consumption. The second wave of FDI is characterized by a two-fold shift: from 
low knowledge base industries to medium and highly knowledge-intensive 
industries and geographically from the coastal and southern areas of the country 
inward. This seems to indicate a less coastal centric stage of development and a 
more domestic one. Are these trends clear at this time and how certain is this 
change in economic development?</b><br>
The publicly available statistics are patchy and present a fragmented and fuzzy 
picture. It is always important to develop a holistic and complete picture as 
more and more statistics emerge, otherwise there is danger of presenting an 
incomplete and contradictory viewpoint.<br>
<br>
Much of the current production in China has been organized by foreign companies 
themselves. They dominate the industry and overseas companies accounted for 87 
per cent of China's 2004 exports. The next 5-10 years will be critical as many 
foreign companies will cut their expatriate staff to reduce costs and many 
companies will also lose their special tax status over this period. Currently, 
state-owned Chinese companies have access to 300,000 engineers who graduate from 
universities every year. However, foreign companies tend to attract the best 
talent. Many state owned Chinese companies are initially likely to rely on a 
fast-growing domestic market before venturing into high technology export 
markets.<br>
<br>
<b>In China, the non-state sector contributes 74 per cent of industrial output, 
62.2 per cent of GDP and more than 100 per cent of the increase in employment. 
Chinese economy seems to have already transitioned much more to a private 
business model than most people outside of China realise. Is this change as deep 
as the above statistics suggest and, how different is China today than many 
European countries in terms of state control and state intervention in the 
economy?</b><br>
The emergence of the non-state sector is a phenomenon that has been mentioned by 
a number of analysts and observers. For instance, recently the Party Congress in 
Beijing has allowed members of the private sector to come into the Standing 
Committee. This is a positive step forward, and does indicate a trend towards a 
more open, diverse economy. While it is too early to say, it is possible to 
imagine a situation in which the Chinese economy is as diverse as and possibly 
more flexible than some of those in Europe today. <br>
<br>
On the other hand, China is already a much more open market than Japan or South 
Korea were when they started to become global industrial powers. China's 
barriers to entry are still falling as it attempts to comply with its promises 
in 2001 to the WTO. <br>
<br>
In contrast, the pace at which China is opening sectors gives local companies 
very limited time to develop their R&amp;D competences and hence innovative 
products, which can compete with western companies.<br>
<br>
<b>Give five reasons for a company to consider establishing a factory or 
business office in China.</b><br>
1. Low labour costs but committed workforce.<br>
2. Chinese culture is similar to Japanese and Korean and shares similar work 
ethics.<br>
3. Huge potential for local markets as well as opportunities for export to 
neighbouring Asian countries and the West.<br>
4. Relatively well developed infrastructure when compared to its competitors 
such as India and Indonesia etc.<br>
5. Increasingly educated workforce. For example, in 2004, Chinese universities 
produced more than 200,000 graduates in computing and information systems and 
the government estimates that between 1978-2003, 700,000 Chinese studied abroad.<br>
<br>
<b>How important is an understanding of the regions and provinces of China to a 
company's plans in China? </b> <br>
Location is always one of the most important decisions, which a company has to 
make as it has significant impact on the returns on investment. It needs to be 
considered that the developments in China are highly divided; some regions are 
more prosperous than others. <br>
<br>
In Shanghai, over 50 per cent of the students have finished higher education. 
However, the level of progression of a region, including economic development, 
infrastructure, convenience of transportation, can often translate into 
investment costs and returns. Additionally, the level of development in one 
region often has high correlations to the culture, educational attainment, 
attitude and the policies of the local government.</font></p>
