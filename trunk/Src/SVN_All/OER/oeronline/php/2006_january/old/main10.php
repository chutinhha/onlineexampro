<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>China is laying 
the groundwork to become a global power in more sophisticated, 
technology-intensive industries that demand considerable capital. Billions of 
dollars are pumped in. How will this affect manufacturing in Europe and North 
America?</b><br>
The advances in Chinese manufacturing present two challenges for producers in 
developed countries. First, more companies and industries will face new 
competition from low-cost Chinese manufacturers. Second, as China becomes more 
self-sufficient, it may limit the opportunities for these companies and 
industries to sell products into the Chinese market. <br>
<br>
In contrast, China's success has two shortcomings: despite great effort to climb 
up the value ladder, the vast bulk of Chinese electronics manufacturing is 
focused on relatively low-value parts production or assembly. Secondly, the 
biggest beneficiaries so far have been overseas ventures.<br>
<br>
Furthermore, it has been argued that if a multi-national company can come and 
take advantage of lowers costs, what can a Chinese company do that is different 
or better. It is felt that a large proportion of the Chinese educated labour 
force lacks creativity. The system is heavily examination-oriented even at the 
top universities.<br>
<br>
I feel that the Chinese have not figured out how to capitalize on their culture 
in terms of economic wealth generation. Once this happens, then the West should 
be concerned. Similarly, Chinese industry still lacks technical know-how and 
competence to compete with the best in the world.<br>
<br>
<b>Do you believe that Chinese manufacturing will drag down the profitability of 
global industries?</b><br>
As China moves into more sophisticated industrial sectors, Western companies are 
likely to face new challenges. They will have to move into even higher-tech, 
higher-value added materials and products - or buy them from China! <br>
