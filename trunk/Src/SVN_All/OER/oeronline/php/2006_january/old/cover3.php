            <p align="justify"><font face="Verdana" size="3">
            <b>I</b></font><font face="Verdana" size="1">ndia’s relationship 
            with Oman is not new. It is characterized by regular interactions 
            during the last 5,000 years. Archeological findings in Oman point 
            towards existence of maritime links between the two countries as 
            back as during the Indus Valley Civilization. <br>
            <br>
            The Indian business community has been continuously present in Oman 
            for several centuries. <br>
            These bonds of age-old friendship were formalized in the modern era 
            with a Treaty of Friendship, Commerce and Navigation, signed between 
            India and Oman in 1953. It was followed by the opening of the Indian 
            Consulate in 1955, one of the first Indian diplomatic 
            representations in the region. <br>
            <br>
            During the last 50 years, India and Oman have entered into a number 
            of bilateral agreements providing for cooperation in fields of 
            culture, economic trade and technical cooperation, avoidance of 
            double taxation, bilateral investment promotion, hydrocarbons, civil 
            aviation, agriculture, science and technology, defence, extradition 
            and on cooperation between state audit institutions. <br>
            <br>
            The mechanism of regular-high level consultations has helped both 
            countries develop a better mutual understanding. Our relationship is 
            based on cooperation derived from respect and appreciation of each 
            other’s traditions, values, capabilities and requirements. India and 
            Oman have played important roles at various international platforms 
            with mutual understanding and cooperation. Both are the founding 
            members of the Indian Ocean Rim Association for Regional 
            Cooperation.<br>
            <br>
            A sizable Indian community comprising businessmen, professionals and 
            other categories of skilled workers present in Oman is cementing 
            this friendship by constructively contributing towards the 
            development of Oman. A number of Indian companies operating in a 
            variety of fields like IT, construction of turn key projects, 
            banking and finance, technical consultancy etc. are present in Oman 
            in order to be the Sultanate’s partners in progress. On the other 
            side, many Omani companies have joint ventures in India in various 
            fields. <br>
            <br>
            In 2005-06, we are celebrating the 50th anniversary of the 
            establishment of diplomatic relations. While many programs have 
            already been organized to commemorate the occasion, few more events 
            are likely to take place in 2006.<br>
            <br>
            My expectations from the year 2006 are very optimistic. Bilateral 
            trade between the two countries, which stood at US$ 351.6 millions 
            in 2004, is likely to grow steadily in the coming year with an 
            increase of investment opportunities in Oman. <br>
            <br>
            The US$ 969 million mega fertilizer plant OMIFCO, which is a joint 
            venture between India and Oman, will be inaugurated in late January, 
            2006. OMIFCO will export urea and ammonia to India worth a quarter 
            billion dollar every year. The Second India-AGCC Industrial 
            Conference will be hosted by Oman in March. <br>
            <br>
            The increasing price of oil has also increased the investment 
            opportunities for Oman. A number of large Indian business houses are 
            exploring business opportunities in Oman with the possibility of 
            investment as the Renaissance reign of HM Sultan Qaboos has highly 
            improved the business environment in Oman. All these developments 
            will further enhance our already strong business relationship.<br>
            <br>
            On the political level also, I expect the bilateral relations to 
            continue the steady path of growth. The forthcoming visit of India’s 
            Defence Minister to Oman at the invitation of his Omani counterpart, 
            who recently visited India, and the fifth session of India-Oman 
            Joint Commission, likely to be held in the first half of 2006, would 
            certainly further elevate our bilateral political relations. <br>
            <br>
            The cooperation between the two countries on various international 
            and regional matters is expected to scale further heights as it is 
            based on the mutual understanding and constructive cooperation 
            towards the common goal of making the world a better place to live.
            <br>
            <br>
            At present, over 1500 Omani nationals are pursuing various academic 
            and professional courses in India, many of them studying on the 
            scholarships provided by the Indian government. This number is 
            likely to go further up as the recognition of Indian higher 
            education and training, which is at par with professional education 
            anywhere in the world, is steadily growing in Oman. While the desire 
            to be involved in the progress and development of Oman is likely to 
            attract more Indian professionals to Oman, the growing tourism, 
            medical facilities and outsourcing opportunities in India are bound 
            to draw more and more Omanis towards her.<br>
            <br>
            Though various Indian cultural events are being organized in Oman 
            from time to time by several private bodies, we plan to organize 
            some more cultural programs like performances, exhibitions, film 
            shows etc. in order to present India’s cultural diversity and 
            richness. <br>
            <br>
            In a nutshell, the bilateral relations between India and Oman are 
            likely to be strengthened further in the year 2006 and I expect to 
            see some more concrete developments in this direction, especially in 
            the area of bilateral trade and technical cooperation.</font></p>
