                <p align="justify"><font face="Verdana" size="1">
            <b>Bilateral trade between India and Oman is all set to grow 
            steadily in 2006, with an increase of investment opportunities, says 
            HE Ashok Kumar Attri, the Indian Ambassador</b></font></td>
                <td width="19%">
                <p align="center"><i><b>
