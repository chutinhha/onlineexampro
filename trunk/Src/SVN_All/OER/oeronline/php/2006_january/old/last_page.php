                        <b><font color="#FF0000" size="4"><br>
                    Well done, but…</font></b></font></p>
                    <p align="justify" dir="ltr"><font face="Verdana" size="1">
                    <img border="1" src="ramesh.jpg" align="left" hspace="5" vspace="2"></font><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
                    Omani economy is on a roll. It is no empty declaration when 
                    none other than the International Monetary Fund (IMF) is the 
                    source of such positive news. After putting the Central Bank 
                    of Oman (CBO) and the Ministry of National Economy through 
                    the grind for weeks together, the Washington-headquartered 
                    multilateral agency did not mince words at the conclusion of 
                    2005 Article IV consultation with the Sultanate of Oman. 
                    “The fiscal budget recorded a surplus of 5 per cent GDP in 
                    2004. Although total expenditures rose by 13 per cent, 
                    higher oil revenues more than matched this increase. Part of 
                    the fiscal surplus was used to build the reserves of the 
                    State General Reserve Fund,” said the IMF a few weeks ago.<br>
                    <br>
                    Despite singing such paeans in glory of Oman, the IMF chose 
                    to speak its mind on some sensitive areas as well. For 
                    instance, the existing full convertibility of Omani Rial and 
                    the pegging to the US Dollar has warranted some 
                    observations.Noting that the present exchange rate peg 
                    “continues to be supported by strong fundamentals and sound 
                    financial policies” which provide an adequate anchor for 
                    “price stability and market confidence”, the IMF pointed out 
                    to the Omani authorities that the “effectiveness of monetary 
                    policy is very 
                    limited”. Simply put, the multilateral body is advising for 
                    something more to guard against any contingency.</font></p>
                    <p align="justify" dir="ltr"><font face="Verdana" size="1">As a palliative, it has suggested some measures that would 
                    lead to fiscal retrenchment as and when inflationary 
                    pressures and capital inflows were to pick up. Luckily, Oman 
                    has conceded that it would favourably consider such 
                    measures. Possibly, the CBO, that conducted the customary 
                    IMF Article IV consultation exercise, is of the opinion that 
                    the genuine concerns on this front would get redressed as 
                    and when the planned GCC monetary union fructifies. Even the 
                    CBO’s concession to review the pass through effects of 
                    exchange rate changes on domestic inflation has gone down 
                    well with the IMF.</font></p>
                    <p align="justify" dir="ltr"><font face="Verdana" size="1">Oman’s privatization programme is another area that caught 
                    the attention of the visitors from Washington. The ongoing 
                    efforts to improve the legal and regulatory framework for 
                    private sector-led growth did not go unnoticed. Yes, it was 
                    well received. However, the Omanization agenda caused a few 
                    flutters, it appears. During the IMF-Oman dialogue, Oman was 
                    advised “to apply their Omanization progamme flexibly, 
                    taking into account both the economic and social dimensions 
                    of this issue.” Subtly, Oman was told to pursue Omanization 
                    targets as broad guideposts rather than “strict numeral 
                    objectives”. <br>
                    <br>
                    It is no secret what the IMF is driving at. It has become 
                    customary for CBO to proclaim its success in the 
                    implementation of Omanization in its annual report every 
                    year that is presented to the nation. For instance, in the 
                    Annual Report 2004, the CBO states that while employment of 
                    Omanis rose by 5.1 per cent to 104,141, that of expatriates 
                    fell by 4.5 per cent to 22,890 and the steady decline in the 
                    number of expatriates in the recent year indicates sustained 
                    notable progress being achieved on Omanization programme.
                    <br>
                    <br>
                    Nonetheless, the IMF perhaps strongly feels that such stiff 
                    yearly targets may not be advisable in Oman’s own interest. 
                    In the absence of trained manpower, aiming for numerical 
                    targets will slow down growth prospects, particularly in the 
                    emerging globalized scenario, where Oman itself is angling 
                    for increased foreign direct investment (FDI) and export 
                    markets. <br>
                    <br>
                    The IMF has advised Oman to freeze the Omanization quota at 
                    the present levels temporarily and lay greater emphasis on 
                    training and education programmes. The IMF has a point worth 
                    considering. Until training of Omani workforce is well 
                    advanced, don’t push hard on Omanization. Given the fact 
                    that the Sultanate is earnestly pursuing the task of 
                    improving the skill sets with overseas assistance, the goal 
                    of greater employment opportunity for its citizens is just 
                    temporary. Nothing less, nothing more!</font></p>

