in the light of historical facts, Oman and India are very old trade partners. In 
the words of Pradip Madhavji, former Chairman of Thomas Cook India Ltd., 
“History has proven that the foundation of Indo-Oman bilateral trade was built 
almost 5,000 years ago.” And now, in the age of globalisation, the two old Asian 
allies seem to be building on their bilateral trade ties. <br>
<br>
The year 1996 had seen the formation of the Indo-Oman Joint Business Council 
(JBC). The Council’s first meeting took place in Muscat. The same council 
recently met for the sixth time in Hyderabad, under the auspices of the 
Federation of Indian Chambers of Commerce and Industry (FICCI). <br>
<br>
The high-profile Omani business delegation was led by Jamil Ali Sultan, Chairman 
of the Oman India Joint Business Council. The Omani delegation also had the 
presence of HE Sausan Ahmed Jaffer, representing the Sultanate’s Ministry of 
Health. The Indian side was represented by senior government officials and 
prominent industrialists.<br>
<br>
The one-day meeting saw some serious brain storming, particularly on the need to 
tap the real bilateral trade potential between the two countries. Between 
2000-2001 and 2004-2005, bilateral trade between the two countries has 
fluctuated considerably . (See Table: Potential Galore) Presently, it stands in 
the range of nearly US$280 million and both sides had this feeling that it is on 
the lower side. So after gauging the existing hurdles, some fresh initiatives 
were mooted. </font></p>
<p align="center" dir="ltr">
<img border="1" src="in_the_news2.jpg" width="318" height="116"></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">Concrete outcome: The most concrete outcome of the meeting was the announcement 
to set up an independent commercial arbitration committee, which will seek to 
resolve the disputes between the business sides of India and the Sultanate. “ It 
will be set up as soon as possible,” Jamil Ali Sultan said after the meeting, 
while pointing out that this was imperative to give an impetus to bilateral 
trade.<br>
<br>
The Omani side also too did some hard-selling stressing joint-venture (JV) 
route, especially in new economy areas like information technology where India 
is a global leader. Other sectors like tourism, education and healthcare were 
also discussed as potential areas where mutual business interests can flourish.
<br>
<br>
Jamil Ali Sultan sent the invitation to Indian entrepreneurs in these words: 
“Here is an opportunity for the Indian businessmen to enter the growing Omani 
market and through it the Middle-East and European markets.” The Omani 
delegation also clearly stated the five-year tax holiday benefits, which new 
entrants in Oman can take advantage of, and the fact that the period can be 
extended. Apart from the JV route, Jamil also highlighted the venture ownership 
provisions available to any big time player. “We allow 100 per cent foreign 
ownership in strategic initiatives and 75 per cent in other cases,” he said. <br>
<br>
The Indian side too expressed hopes that given the dynamics of the two economies 
and the demands of the changing times, further improvement in bilateral trade 
was very much on the cards. Said Madhavji, who participated in the meeting: 
“Indo-Oman cooperation can further pick up through diversification of trade, 
joint ventures and industrial tie-ups.” - Ritwik Sinha</font></p>
