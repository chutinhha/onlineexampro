                            <p style="margin: 5px 10px" align="justify"><b><font face="Verdana" size="1">Friday blues</font></b><font face="Verdana" size="1"><br>
            Friday is the only certain ‘day off’ in the Gulf. Those who 
            telephone Gulf expatriates at home on Fridays should be sensitive to 
            this fact for two reasons. The first is the simple reason that most 
            Gulf expatriates need their day off and the majority do not 
            appreciate being disturbed for reasons of work on their ‘Sunday’. 
            The second reason is more subtle: if ‘head office’ cannot even 
            understand that Friday is not a Gulf working day, what other more 
            important confusions and misjudgements are being made about the 
            nature and pressures of work in the Gulf? “What else are they 
            getting wrong about me and my work here?”<br>
                            <br>
            The rule for ‘head office’, if work really has to be conducted with 
            Gulf staff on a Friday, is to apologize first so that the expatriate 
            can take some minor comfort from the fact that the person 
            telephoning at least appears to know about Fridays. It is not 
            unknown for expatriates deliberately to respond in kind by 
            telephoning their ‘head office’ colleagues at their homes on Sundays</font></td>
