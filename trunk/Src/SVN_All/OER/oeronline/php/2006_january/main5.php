</font><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="5" color="#FF0000">‘Planned integration is the 
key’</font></b><font face="Verdana" size="1"><br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="in_the_news4.jpg" width="200" height="205" align="right" hspace="5" vspace="2">W</b></font><font face="Verdana" size="1">ith 
a clear-cut strategy of expanding regional economies and the</font><font face="Verdana" size="1"> 
neighbouring regio</font><font face="Verdana" size="1">n, Dubai Poly Film Co. 
LLC, owned by Al Ghurair Limited and SHUAA Capital, is charging ahead in its 
growth path towards greater targets. Recent acquisitions in Oman and Egypt are 
only part of the company’s larger plans. <br>
<br>
After the recent takeover of Oman-based Al-Khaleej Polypropylene Company SAOG, 
Rashed Seif Al Ghurair, MD of Dubai Poly Film, spoke to Clarence Michael in an 
exclusive interview at his Dubai headquarters. Excerpts:<br>
<b><br>
Is your Oman acquisition part of the Dubai Poly Film’s strategy to be in the Top 
10 list in this sector?</b><br>
Yes, absolutely.<br>
<br>
<b>What kind of synergy this acquisition is expected to bring in to your 
existing operations? Are you planning more such acquisitions?</b><br>
The acquisition in Oman should provide greater synergies in the operations of 
both organizations, improving economies of scale and sharing best practices to 
hopefully enhance competitiveness and deliver improved quality and service to 
our customers. Dubai Poly Film has an objective of being a leader in packaging 
films and this does not exclude further acquisitions, if required, to achieve 
the planned growth targets.<br>
<br>
<b>Once the rest of the acquisition procedure goes through in Oman, will there 
be any structural changes at the management level? With your sizeable strength 
as a regional group, how do you plan to strengthen the current team of 
professionals at Al Khaleej Polypropylene?</b><br>
As with any such acquisitions, a detailed and planned integration process must 
be implemented. Following the completion of the acquisition process, Dubai Poly 
Film and Al Khaleej Polypropylene Products will work together to enhance the 
capabilities of both the organizations in all aspects, including the 
establishment of a strong and effective management team.<br>
<br>
<b>Do you feel the remaining shareholders will accept your offer to smoothen the 
rest of the acquisition process? </b><br>
We are confident that the remaining shareholders will realise a fair and 
reasonable return on their investment.<br>
<br>
<b>Will the shares of Al Khaleej continue to be listed on the Muscat Securities 
Market after the acquisition is completed? </b> <br>
This has not been decided as yet.<br>
<br>
<b>What are your plans for Technopack in Egypt? </b><br>
Technopack is a market leader in BOPP films in the Egypt and North African 
markets. The integration of Techno Pack and Dubai Poly Film will enhance the 
capabilities of both operating units to enable each to expand the range of 
products and expand the markets served.<br>
<br>
<b>Your current annual sales (Dubai Polyfilm) are in the region of US$120 
million. What kind of growth do you expect this year and in the coming years?
</b><br>
We expect to grow at a rate in line with the growth of demand for BOPP films in 
the markets currently served and achieve above market growth through sales 
expansion into new regions/territories. In addition, our product development 
programme will provide a stream of new products, which will both enhance the top 
line and improve returns.<br>
<br>
<b>Are your facilities state-of-the-art currently?
<img border="1" src="in_the_news5.jpg" width="150" height="121" align="right" hspace="5" vspace="2"></b><br>
Our facilities are state-of-the-art and, on an average, newer than those in more 
mature markets such as the United States and Europe. We cannot exclude any 
cooperation - technical or commercial - with third parties where this enhances 
the ability of Dubai Poly Film to achieve its objectives and adds value to the 
business.<br>
<br>
<b>What does future hold for the regional BOPP film packaging sector?</b><br>
On the demand side, the future for packaging films in the region is very bright. 
We are in a region with high economic growth and with a young and growing 
population, which will increasingly consume those items packaged in BOPP. On the 
demand side, there is a need to rationalise the downstream printing and 
converting industries. This, we feel, is inevitable as the major food companies 
demand higher qualities in both products and services. These higher standards 
will force out the weaker and smaller players, leaving the stronger and more 
capable companies to prosper.<br>
<br>
<b>Do you see any consolidation at the global level among the major players in 
the industry?</b><br>
Yes, the global BOPP industry is currently very fragmented and we see signs of 
consolidation, which we anticipate will increase over the coming few years.<br>
<br>
<b>Is there stiff competition regionally for your products?</b><br>
As in any industry these days, competition is fierce. Regional competition is 
being replaced by global competition. Dubai Poly Film was faced with having a 
strong position in the Gulf markets. However, it faces increasing competition 
from outside the region, such as India and China. <br>
<br>
Therefore a decision was made to expand the company to become a global player. 
Being based the Middle East, it is obvious that Dubai Poly Film enjoys a very 
positive business environment. The Gulf region, Dubai in particular, is a very 
competitive region from which to be based, enjoying competitive labour and raw 
materials. In this environment and with the capabilities within the company, 
Dubai Poly Film can compete on a global basis as well as defend its leadership 
in local markets.<br>
<br>
<b>Which areas do you focus for your export growth? Which are the promising 
markets for your products now and for the future?</b><br>
The attractiveness of specific markets depends on many variables, which are in 
constant change. However, we are well positioned to increase revenue from 
markets in Europe, Africa and the Indian subcontinent.
