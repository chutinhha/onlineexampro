<p align="justify"><font face="Verdana" size="2" color="#C0C0C0">:: EVENT OF THE 
MONTH<br>
<br>
</font><b><font face="Verdana" style="font-size: 16pt" color="#FF0000">Toyota in 
overdrive with new showroom</font></b></p>
<p align="center">
<img border="1" src="ene_f_t_mon.jpg" width="514" height="180"></p>
<p align="justify"><font face="Verdana" size="3"><b>N</b></font><font face="Verdana" size="1">ot 
taking its leadership in the auto market in the Sultanate for granted, Saud 
Bahwan Group (SBG), in a high-voltage launch event on December 4, 2005, unveiled 
its largest and most modern showroom (which took 18 months to complete) for 
selling Toyota vehicles. <br>
<br>
Appreciating the aggressive business strategy of SBG, Dr. S. Toyoda, honorary 
Chairman, Toyota Motor Corporation, the special guest of honour, said: “Thanks 
to Chairman Saud Bahwan’s huge investments, innovative marketing efforts and his 
commitment to improving customer services, Toyota’s sales volume and market 
share in Oman has increased rapidly. I understand that the volumes this year 
would be close to 50,000 units, which is an all-time record. It is remarkable 
that Toyota enjoys a market share in Oman, that is even higher than in Japan.”<br>
<br>
<b>Magnificent launch </b><br>
Leaving no stone unturned, SBG launched the new showroom at a grand event under 
the patronage of His Highness Sayyid Haitham bin Tariq Al Said. The dignitaries 
included His Highnesses, His Excellencies, ambassadors, top corporates and media 
owners etc. The launch event was managed by a UK-based global event management 
specialist agency, which won the contract competing with over half-a-dozen 
top-level global event management agencies. Working closely with the marketing 
team of SBG on Toyota brand, the agency meticulously conceptualized and managed 
the event, which was among the most high-profile and spectacular launch events 
in the Middle East. A special team of performers and artistes, flown in from 
different parts of the world, made the launch a lifetime experience for the 
distinguished invitees. <br>
<br>
To start with, two teams of special contortionists and performers, enthralled 
the audience with their scintillating display of perfect body movements and 
balance. This was followed by the complete frontage of the showroom becoming the 
screen of a film. A team of dancers, using the façade of the showroom as the 
surface, performed a stunning vertical aerial ballet with special laser and 
sound effects. Even the inauguration of the showroom was done in an innovative 
way, with three special performers sliding down from the top of the building, 
unwrapping a red inaugural ribbon, which was symbolically cut by HH Sayyid 
Haitham and Dr. Toyoda. <br>
<br>
The next stage of the launch event was set in a Japanese ambience at Al Bustan 
Palace Hotel. The invitees were entertained by distinguished Arabic vocalist 
Najwa Karam and a Japanese music band. Dr. Toyoda presented a special, ‘Toyota 
Excellence Award’ to Saud Bahwan, chairman of Saud Bahwan Group, in appreciation 
of his efforts to take Toyota to new heights in the Sultanate. <br>
<br>
He further added: “It is a great honour for me to attend this auspicious 
ceremony to commemorate the 30th anniversary of the business relationship 
between Saud Bahwan Automotive and Toyota Motor Corporation. Here in Oman, we 
shall reinforce the Toyota and SBA partnership and endeavour to give 
satisfaction, in all areas, to customers and the society, in line with our motto 
‘Mutual Trust’. We hope these efforts will retain Toyota’s position as the most 
successful and respected car company in the Oman market for years to come.”<br>
<br>
<b>Showroom marvel</b><br>
Located in Wattayah, the new Toyota showroom showcases an excellent blend of 
human engineering and creativity on a very large scale. SBG has especially 
imported two robots to welcome the customers in English and Arabic language. OER 
is informed that the two robots are programmed to greet and converse with 
customers in a human like manner. An SBG spokesperson said: “The two floors of 
the sprawling showroom are divided into different zones that cater to the 
varying needs of customers and provide them an unmatched service to make them 
feel delighted.”</font></p>
<p align="center">
<img border="1" src="ene_f_t_mon1.jpg" width="514" height="180"></p>
<p align="justify"><font face="Verdana" size="1">The ground floor is divided 
into the passenger car (PC) zone, the 4-wheel drive (4WD) zone and the youth 
zone. The PC zone displays the range of passenger car models. As a special 
display, Toyota’s ‘Fine-S’ hybrid car, which emits only water vapour, is also 
showcased in the zone. <br>
<br>
The 4WD zone is meant for the auto enthusiasts who like rugged adventure. The 
Land Cruiser, Fortuner, Prado, RAV 4 etc. are on display here. A 30-year-old 
vintage Land Cruiser in a good running condition is also on display (not for 
sale) here to strengthen Toyota’s brand promise of lasting quality and 
reliability. <br>
<br>
To match the intensity and preference of the burgeoning younger age group, the 
youth zone offers a range of Toyota’s sporty models set in a colourful and 
fast-paced ambience. The original Toyota Formula 1 car driven by Ralph 
Schumacher and Jacob Trulli is displayed to set the mood of the young prospects. 
To pump up their adrenalin, the zone has a Simulator to provide a real hands-on 
feel of a Toyota in action. “One can actually experience what it feels like to 
be hurtling down a valley, bashing through a Wadi and zooming at 
terrain-blurring speeds, without stepping out of the showroom,” informed a SBG 
spokesperson. <br>
<br>
The basement of the showroom is used for new vehicle deliveries and special 
lounges where customers can wait while taking delivery of their vehicles. 
Adjacent to the showroom is the test drive area for those who wish to get a 
first hand feel of the Toyota range. By the side is the trade-in area where 
customers can exchange their old cars while buying new cars. <br>
<br>
<b>State-of-the-art warehouse</b><br>
Coinciding with the showroom launch, SBG also inaugurated its new warehouse, 
which is among the largest warehouses ever built for Toyota. The new parts 
distribution centre is spread over 55,000 sq. metres, using the modern 
technology for smoother and efficient operations in place. According to an SBG 
official, “The totally paperless operations are managed by computer systems with 
mobile devices connected by wireless RF network. The new facilities will help 
SBG achieve an altogether new level of customer satisfaction with efficient, 
accurate and safe operations.”<br>
<br>
With the auto market in Oman being a relatively small market, what have been the 
objectives behind making such significant investments in the new facilities and 
high decibel launch event? An SBG spokesperson responded: “The customers have 
always been an active partner in our progress. In order to keep our customers 
happy, we need to ensure that they get the best care as per the highest 
standards in the perfect environment that is designed and paced at their 
convenience. Our fresh investments are part of our endeavour to take the service 
standards to a new high, much beyond the expectations of our customers and set 
new world class market benchmarks.” <br>
<br>
<b>Way forward</b><br>
As Toyota is expected to march ahead globally, SBG is striving to take Toyota’s 
numero uno position to even greater heights in the Sultanate. “The launch of the 
new showroom and the ware house is just a beginning of our strategy to further 
strengthen Toyota and enrich the customer’s experience with the brand in various 
ways,” said an SBG spokesperson. The group has also embarked on an exercise to 
upgrade its 25 Toyota showrooms across the Sultanate. - Akshay Bhatnagar</font></p>

