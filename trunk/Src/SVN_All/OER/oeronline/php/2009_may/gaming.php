                the PlayStation in 1996. From introducing the ‘Survival Horror’ 
                genre, with the first Resident Evil, till the entire shift in 
                perspective in RE4, the series has always delivered in terms of 
                gameplay, terror, characters and story... except maybe for the 
                lacklustre side-story titles. Now, with the release of Capcom’s 
                latest RE title, Resident Evil 5, they hope to build on this 
                success. <br>
                <br>
                To put it simply – the game is no longer the terror-inducing 
                zombie-slaying game that you are used to. What Capcom has done 
                is to replace this ‘edge of the seat’ terror to ‘edge of the 
                seat’ action and tension. But this is also where the good news 
