                Instead, it evolves and builds on an already impressive game 
                mechanic that the latter introduced. The game is still played in 
                the same way as RE4, in an over the shoulder perspective, and 
                just like in the previous title when you aim you can no longer 
                move and you use the laser sight as guide to aim. This control 
                scheme seems a bit clunky in this day and age and may not be 
                liked by all. But the way the gameplay is tailored around this 
                control system is quite impressive and helpful.<br>
