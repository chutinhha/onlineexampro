            <b>It was the light frying oil Khafeef that gave a new dimension to 
            the concept of cooking oil and helped Areej Vegetable Oils and 
            Derivatives to jump five ranks on the OER Top 20 list. Visvas Paul D 
            Karra catches up with Prem Maker, Executive Director of the company 
            for a tète-à-tète<br>
            </b>
            <br>
            <img border="0" src="cover15.jpg" align="left" hspace="8" vspace="6" width="250" height="379">The 
            year 2008 was a roller coaster year for all commodities. Like the 
            prices of oil, the prices of vegetable oils also shot through the 
            roof during the first half of 2008, peaking in July just as the US 
            sub-prime effect began to extend its vice-like grip on the global 
            economy. But beginning in September 2008, the commodity prices 
            started tumbling and troughed by almost 45 per cent by December. 
            While companies slowed down in line with changing market dynamics, 
            Areej Vegetable Oils &amp; Derivatives (AVOD) moved smoothly ahead like 
            a well-oil production machine.<br>
            <br>
            “The vegetable oil market followed the petroleum oil story and other 
            commodities. Therefore the key was to manage purchasing and buyings 
            to suit the market rather than the interests of speculators. I think 
            we managed it extremely well and finished 2008 with good topline and 
            bottomline results,” says Prem Maker, Executive Director of AVOD. 
            The company’s revenues soared to RO90.68mn in 2008 as compared to 
            RO53mn showing a year-on-year growth of more than 71 per cent.<br>
            <br>
            <b>The ‘Khafeef’ storm<br>
            </b>Apart from the high commodity prices, there are many factors 
            that have contributed to the phenomenal growth of AVOD. One of the 
            most significant features was the launch of corn-oil based frying 
            oil Khafeef Oil in August 2008. At about the same time as the global 
            financial crisis began hitting companies hard, Areej Vegetable Oils 
            took the northern route by introducing its revolutionary product 
            innovation – Khafeef – the lightest frying oil in the GCC. This 
            specialised frying oil was packed in a stylised bottle, which 
            typifies the image of a woman, who is the ultimate user of the 
            product. Explains Maker: “It began with extensive consumer research 
            and evaluation of different oils in actual frying tests conducted 
            with breaded chicken fillet in research labs. Then we invested RO4mn 
            in plant and packaging machinery to deliver on the new technology 
            for this new oil. The finished product, Khafeef, packaged in a 
            beautiful PET bottle, took the market by storm.”<br>
            <br>
            <b>Market leader</b><br>
            Areej Vegetable Oils was established in 1980 with a majority 
            shareholding of the OMZEST Group. It has a modern hygienic plant 
            located at Rusayl Industrial Estate to manufacture and market top 
            quality cooking oils, ghee, margarine, specialty fats and butter 
            products.<br>
            <br>
            The company’s leading brands Minara, Sohar, Khafeef, Jabal Akhdar 
            and Muscat Margarine are trusted names in every Omani home. Areej 
            Vegetable Oils enjoys a 65 per cent market share in Oman and current 
            exports are 70 per cent of the total business. The company exports 
            to 23 countries around the world. Says Maker: “We have done better 
            than our competitors basically because we do not speculate. We 
            follow the basic economics of supply and demand and that is why when 
            the crunch period came, we were not affected seriously. We are 
            manufacturers of a product not speculators.”</font></p>
