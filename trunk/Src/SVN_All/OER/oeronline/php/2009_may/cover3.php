            <b>Innovation is the key<br>
            </b>Success has followed AVOD mainly through its innovative products 
            and packages which have attracted the customers. Some of the 
            innovations done by the company include the bag-in-carton cooking 
            oil that was a huge success in Oman and UAE. ‘Sohar’ was the first 
            blended oil in the GCC to suit Omani frying habits. ‘Minara’ was the 
            first cooking oil in a crystal clear bottle to make an entry in the 
            GCC market. Furthermore, Minara was the first PET bottle in the GCC 
            with a handle. Exports also got a major fillip with the manufacture 
            and sale of private label brands in PET bottles with handles.<br>
            <br>
            Maker attributes the success of the company basically to a market 
            hunch or in other words matching your business plans to market 
            realities. “We have controlled our cash flows and stocks and we have 
            always been realistic in our expectations, as I had said earlier. 
            Khafeef is a prime example of catering to the market realities and 
            we have achieved tremendous success with this product,” says Maker.<br>
            <br>
            <b>Cost, price effective<br>
            </b>The company continues to grow in new markets with products such 
            as ghee in tins, bag-in-box; polyjars cooking oil in tins; blended 
            oil in clear pet bottles; specialty oils and shortenings etc. 
            Multinationals like Pepsico (Snacks), Unilever and Emborg source 
            their requirements for the Gulf from Areej Vegetable Oils. <br>
            <br>
            Besides product innovations, AVOD has increased its investments in 
            modernisation of plant and upgrading of technology in order to 
            deliver cost effective products to the customers thus delivering 
            more returns to the shareholders. Riding high on its new-found 
            euphoria, Areej Vegetable Oils tasted sweet success again. <br>
            <br>
            While the world shrank into recession, the company expanded its 
            geographical reach to the non-GCC markets and made inroads into Iraq 
            in 2008 itself and increased its market share there by 35-40 per 
            cent. In Oman, AVOD has six major brands of cooking oils, two brands 
            of ghee, and two margarines.</font></p>
