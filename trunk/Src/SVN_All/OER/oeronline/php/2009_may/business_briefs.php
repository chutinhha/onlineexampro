                Starts work on Main Spine Line</b></font></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                <img border="0" src="bus2.jpg" align="right" hspace="8" vspace="6" width="252" height="168">Abdullah Al-Hashemi, Director General of Projects at Haya Water 
                Company, announced that the company assigned the task of 
                implementing the Main Spine Line project started work on the 
                installation of the main line by using the technology of micro-tunneling 
                for a total length of 19 kms. in the Wilayet of Seeb. He added 
                that the work had started on the implementation of the first 
                phase of the installation of the main transportation line, and 
                that the new technology depends on the laying of wastewater 
                networks without the need for digging open trenches. The new 
                technology involves the fixing of modern and giant excavation 
                equipment at specified points where horizontal excavation work 
                will take place at such points. About the advantages of using 
                this new technology, Al-Hashemi said that the use of this method 
                in excavation works ensures free traffic and pedestrian movement 
                and reduces any sound arising from conventional excavation 
                machinery and trucks, in addition to reducing pollution from 
                flying dust and without the need to cut down any trees and green 
                patches that obstruct the course of the network.”<br>
                <br>
                The company has started the project by using a single piece of 
                excavation machinery with another piece expected to be brought 
                to the site by the end of this month and three more by the end 
                of August so that the number of giant and modern excavation 
                machinery would total five. About the radius of the pipelines, 
                Al-Hashemi mentioned that the radius of the main pipeline would 
                range between 1.3 to 2.5 meters and that 67 manholes would be 
                constructed along the route of the pipeline in order to 
                facilitate maintenance in future.<br>
