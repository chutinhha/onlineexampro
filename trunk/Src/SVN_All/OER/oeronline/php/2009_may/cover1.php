            the roof during the first half of 2008, peaking in July just as the 
            US sub-prime effect began to extend its vice-like grip on the global 
            economy. But beginning in September 2008, the commodity prices 
            started tumbling and troughed by almost 45 per cent by December. 
            While companies slowed down in line with changing market dynamics, 
            Areej Vegetable Oils &amp; Derivatives (AVOD) moved smoothly ahead like 
            a well-oil production machine.<br>
            <br>
            “The vegetable oil market followed the petroleum oil story and other 
            commodities. Therefore the key was to manage purchasing and buyings 
            to suit the market rather than the interests of speculators. I think 
            we managed it extremely well and finished 2008 with good topline and 
            bottomline results,” says Prem Maker, Executive Director of AVOD. 
            The company’s revenues soared to RO90.68mn in 2008 as compared to 
            RO53mn showing a year-on-year growth of more than 71 per cent.<br>
            <br>
            <b>The ‘Khafeef’ storm<br>
            </b>Apart from the high commodity prices, there are many factors that 
            have contributed to the phenomenal growth of AVOD. One of the most 
            significant features was the launch of corn-oil based frying oil 
            Khafeef Oil in August 2008. At about the same time as the global 
            financial crisis began hitting companies hard, Areej Vegetable Oils 
            took the northern route by introducing its revolutionary product 
            innovation – Khafeef – the lightest frying oil in the GCC. This 
            specialised frying oil was packed in a stylised bottle, which 
            typifies the image of a woman, who is the ultimate user of the 
            product. Explains Maker: “It began with extensive consumer research 
            and evaluation of different oils in actual frying tests conducted 
            with breaded chicken fillet in research labs. Then we invested RO4mn 
            in plant and packaging machinery to deliver on the new technology 
            for this new oil. The finished product, Khafeef, packaged in a 
            beautiful PET bottle, took the market by storm.”<br>
            <br>
