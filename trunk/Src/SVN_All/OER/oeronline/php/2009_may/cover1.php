            require changing products or variants to suit the changing dynamics 
            of the market and customer tastes. Says Ghouse, “You cannot deal 
            with new issues in old ways in a competitive environment.” KR, a 
            company that dates back to 1830 is itself a living testament of 
            adapting to change. For instance, the group has in the last few 
            years repositioned Khimji Mart (K-Marts) as a neighbourhood store, 
            to differentiate it from the large format stores. The other part of 
            the strategy has been to focus on towns outside the Capital area, 
            like Bahla, Sur, Nizwa etc. In the last six years the number of 
            K-Marts have gone up from four to 16. The quality of service and 
            product profile of these stores have made them destination markets 
            in most of these smaller towns. To cite another example, over the 
            last five years, KR has gone from distributing 500 stock keeping 
            units (SKUs) from P&amp;G to 1500 SKUs. This has necessitated a change 
            in the process systems followed by the company. “There are several 
            elements involved in managing the supply chain like the accuracy of 
            forecasting, technology, collecting and analysing the data and 
            communication channels with the principals,” says Ghouse.<br>
