			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>Who is out and who is in</b><br>
            We have two new comers on the list this year. Oman Flour Mills comes 
            into the number 18 spot and Al Hassan Engineering is back in the 
            number 20 slot after missing out last year. These companies have 
            made it to the OER Top 20 this year at the expense of AES Barka and 
            Oman Refreshment.<br>
            <br>
            The ranking of Oman’s 20 largest companies in order of revenue 
            produces a list, which includes the seven companies from the banking 
            and investment sector, six from the services sector and seven from 
            the industrial sector.<br>
            <br>
            There has been no change in the top ten companies in Oman by 
            revenues. Omantel continues to be the largest public company in Oman 
            with a growth in revenue of 12.74 per cent compared to last year.<br>
            <br>
            <img border="0" src="cover92.jpg" align="right" hspace="8" vspace="6" width="220" height="299">The Chairman of Omantel Eng. Sultan Bin Hamdoon Al Harthy in his 
            report to the shareholders explains that the main reason for good 
            growth in net profit is the continuous double digit revenue growth. 
            Al Harthy states that the total group revenue rose by 12.6 per cent. 
            Al Harthy outlines that the total group net profit after tax rose by 
            5.9 per cent to RO118.6mn. Al Harthy adds that the total subscriber 
            base has recorded a growth of 13.4 per cent. The total number of 
            subscribers has increased to 2.119mn compared to 1.869mn last year. 
            Al Harthy goes on to state that due to the global financial crisis 
            and the deteriorating market conditions in Pakistan, the value of 
            their strategic investment in Worldcall Telecom has been affected 
            and the group has recognised an impairment loss of RO18.88mn. 
            Without this impairment the net profit would have been RO137.5mn, a 
            growth
            of 22.8 per cent.<br>
            <br>
            Al Harthy cautions that during the year 2009 the sector 
            liberalisation is likely to intensify with the TRA announcement of 
            awarding Oman’s second fixed line network and possible award of more 
            Internet service providers in 2009. He adds that the government plan 
            to sell 25 per cent of its stake in Omantel to a strategic investor 
            has been called off because of the current conditions in the global 
            capital markets. <br>
            <br>
            <b>Profit index</b><br>
            Similar to last year, three of the top five most profitable 
            companies in Oman are banks. As expected in the current economic 
            climate four of the five companies are showing a small growth in 
            profits for the year whereas one is showing a decline. There is one 
            change in the top five companies by profit. Raysut Cement, which was 
            in the fourth place last year, has moved down to the number five 
            position and OIB is up from five to four. Omantel continues to 
            remain at the number one spot, Bank Muscat at the number two spot 
            and NBO at number three.<br>
            <br>
            Bank Muscat is the second most profitable company in Oman as well as 
            the third largest company in Oman based on turnover for the year 
            2008. Bank Muscat has achieved this position by recording growth in 
            profit for the year of about 11.24 per cent.<br>
            <br>
            The Chairman Abdul Malik bin Abdullah al Khalili states in his 
            yearend report to the shareholders that despite the global financial 
            crisis the key business lines of the bank continued to turn in an 
            impressive performance in 2008. Khalili adds that during 2008, the 
            return on average assets was at 1.8 per cent compared to 2.3 per 
            cent in 2007. The return on average equity was 14.8 per cent in 2008 
            compared to 25.8 per cent in 2007 and the basic earnings per share 
            was RO0.087 as against RO0.090 in 2007.<br>
            <br>
            Khalili explains that their strategic shareholding in Saudi Pak 
            Commercial Bank was impaired due to the deteriorating economic 
            situation in Pakistan and even though this investment is strategic 
            in nature as a measure of prudence the bank has provided for an 
            impairment loss of RO17mn for this investment.</font></p>
			<p align="center" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover93.jpg" width="350" height="408"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Khalili goes on to say that the Board has proposed a dividend of 50 
            per cent, 20 per cent in the form of cash and 30 per cent in the 
            form of mandatory-convertible bonds. He advises that the 2009 
            financial market environment appears to be challenging for business 
            with tight liquidity conditions, weak equity market and tightened 
            lending norms by banks. Khalili is confident that the bank
            will sail through these turbulent times.<br>
            <br>
            <b>Tops turvy</b><br>
            Al Anwar Holdings has shown the highest growth in profits by an 
            enormous 99.86 per cent and has remained in the number one spot for 
            the second year running. Three of the top five companies showing 
            highest growth of profit from last year being Oman Flour Mills, 
            Renaissance and Areej Vegetable Oil are new on the list. Al Jazeera 
            Steel, OMINVEST and Al Maha Petroleum have all dropped off this 
            list.<br>
            <br>
            <img border="0" src="cover94.jpg" align="right" hspace="8" vspace="6" width="279" height="314">The Chairman of Al Anwar, Masoud Humaid Al Harthy in his report to 
            the shareholders for the year ended March 31, 2008 has stated that 
            new business strategies; investment policies and procedures; and 
            risk management tools introduced by the management in the second 
            half of the year has contributed in the continued sustainable growth 
            of the company. The group has recorded 100 per cent growth in 
            profits over last year achieving its highest ever net profit of 
            RO4.33mn for the year ended March 31, 2008. He points out that out 
            of this profit RO1.13mn is generated from disposal of 31 per cent 
            shareholding in NAPCO. The board has recommended a cash dividend of 
            eight per cent and a stock dividend of 12.93 per cent.<br>
            <br>
            Al Harthy adds that the group’s subsidiaries Voltamp Manufacturing 
            and Sun Packaging are all performing well as are its associates 
            NAPCO, Al Anwar Blank, Falcon Insurance, Al Maha Ceramics and Addax 
            Securities Saudi Arabia. Al Harthy explains that the group continues 
            to focus on the financial services and real estate sectors.<br>
            <br>
            Four of the top five companies that have the highest amount of 
            equity employed are banks. Four of the companies in this category 
            remain the same as last year. BankMuscat continues to remain number 
            one in this category. Omantel and NBO continue to remain in the same 
            position as they were last year. Bank Dhofar, a newcomer on the 
            list, comes in at number four knocking OIB down to the number five 
            position. Oman Cement which was number five last year is no longer 
            on this list.</font></p>
