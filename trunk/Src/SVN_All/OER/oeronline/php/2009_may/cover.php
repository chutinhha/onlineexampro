			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">The then chairman of NBO Suhail Salim Bahwan in his report to the 
            shareholders states that the bank’s net profit of RO45.4mn exceeds 
            last year’s record net profit of RO44.6mn despite a very turbulent 
            fourth quarter and after impairment charge of RO3.7mn on 
            investments. Bahwan adds that the profit from operations grew by 49 
            per cent from RO36mn to RO54mn reflecting the bank’s clear emphasis 
            on sustainable growth. He explains that the bank has diversified its 
            income as reflected in ratio of other operating income to total 
            income which increased from 37 per cent in 2007 to 46 per cent in 
            2008. Business efficiency remained an important focus and the 
            improvement in cost to income ratio continues to improve. The 
            recoveries and releases against the provisions for 2008 were at 
            RO16.6mn. Total assets ended the year at RO1.9bn. This growth was 
            linked to the growth in deposits by RO406mn (43 per cent) and growth 
            in net advances by RO494mn (54 per cent).<br>
            <br>
            Bahwan goes on to say that 2009 will pose significant challenges in 
            the international debt and capital markets. The global economy is 
            expected to continue to suffer under the impact of a prolonged 
            recession. In the GCC region, lower oil prices are expected to slow 
            the growth in the region. Bahwan states that the bank will continue 
            to grow in a prudent and profitable manner by targeting quality 
            assets in Oman and the UAE with the overall objective of enhancing 
            shareholder value.<br>
            <br>
            <b>
            <img border="0" src="cover96.jpg" align="left" hspace="9" vspace="6" width="278" height="273">Sea change<br>
            </b>This year, only one of the top five companies that have the highest 
            market capitalisation on the MSM is not a bank. Omantel has taken 
            over from Bank Muscat as the number one this year; BankMuscat has 
            slipped to the number two spot in this category. NBO is a newcomer 
            to this list straight into third place, Bank Dhofar has jumped up 
            one place to the fourth position and OIB is a newcomer in the number 
            five position. Raysut Cement and Galfar who were number three and 
            four last year are both knocked out from this list.<br>
            <br>
            The chairperson of Oman International Bank, Reem Omar Zawawi in her 
            annual report to the shareholders states that 2008 was another 
            successful year for the bank with record profitability. Zawawi adds 
            that the business philosophy of the bank is to continue to deliver 
            consistent returns to the shareholders backed by a determination to 
            pursue growth with prudent risk management.<br>
            <br>
            Zawawi explains that the bank achieved a net profit of RO29.47mn for 
            the year 2008 compared to RO28.07 for 2007, an increase of 4.97 per 
            cent. The bank commands a market share of 8.23 per cent for deposits 
            and 7.4 per cent for loans and 7.77 per cent for total assets as at 
            December 31, 2008 as per the consolidated statistics from the 
            Central Bank of Oman. In the light of these results the board of 
            directors has proposed a cash dividend of 27 per cent.<br>
            <br>
            Zawawi states that the bank looks to the future with conservatism, 
            confidence and optimism. The government policies continue to focus 
            on strong economic growth with expenditure up 11 per cent and 
            revenue by four per cent. In the future the bank will increase its 
            delivery channels in terms of new state of the art branches, ATMs, 
            PoS (point of sales) and customer friendly mobile banking services. 
            She adds that the bank will make optimum use of these channels to 
            redefine customer convenience and thereby acheive continous 
            sustainable growth.</font></p>
