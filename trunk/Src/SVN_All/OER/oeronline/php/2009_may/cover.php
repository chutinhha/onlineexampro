			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b>Oman Economic Review presents its annual report on Oman’s Top 20 
            listed companies for the year 2008</b><br>
            <br>
            <br>
            <img border="0" src="cover88.jpg" align="left" hspace="8" vspace="6" width="220" height="278">For the sixth consecutive year, 2008 has been fairly positive for 
            Oman’s economy in general due to the growth in the first half of 
            2008. The price of oil continued to increase during the earlier part 
            of the year 2008. Oman crude realised an average price of $101.06 
            per barrel in 2008 compared with an average price of $65.15 for 2007 
            and the budgeted price of $40 per barrel for 2008. The original 
            total appropriations for the year 2008 of RO5,800mn were 
            substantially increased by a further RO603mn to implement a number 
            of development and basic infrastructure projects. Due to the higher 
            oil prices the actual budget was projected to close with a budgetary 
            surplus of RO1,581mn as against a projected deficit of RO400mn.<br>
            <br>
            <b>Market meltdown<br>
            </b>However, due to the global financial meltdown, 2008 has been quite 
            challenging for the Muscat Securities Market (MSM). You may recall 
            from our statement last year that due to the bursting of the ‘Credit 
            Bubble’ in a year that was full of bad news for developed markets, 
            emerging markets spent 2007 on another planet, with returns of 35 
            per cent to 40 per cent. This phenomenon continued till the middle 
            of 2008 but hit home with a vengeance in the second half of 2008. 
            The MSM ended the year as the fourth best performing stock market in 
            the GCC with an overall decline of 40 per cent. Two successful IPOs 
            were launched during the first half of the year with unprecedented 
            oversubscription of more than 25 times. Trading volumes amounted to 
            RO3,710mn in 2008 compared to RO2,321mn in 2007, an increase of 59.8 
            per cent. The benchmark MSM 30 index ended the year at 5,441 points, 
            a loss of 39.78 per cent for the year. The top performer was the 
            services and insurance sector which was down 28 per cent followed by 
            the industry sector which lost 45 per cent and the banking and 
            investment sector was the worst hit ending with a decrease of 46 per 
            cent. Foreign participation in the capital of the listed joint stock 
            companies also reduced during the year to 23.84 per cent compared to 
            27.55 per cent last year, a decrease of about 13 per cent.</font></p>
			<p align="center" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="cover89.jpg" width="353" height="416"></font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Due to higher oil prices in the first half of the year, increased 
            non-oil exports and strong demand, the GDP growth rate for 2008 is 
            estimated at 10 per cent. Oil production increased in 2008 by about 
            seven per cent. The government’s continued emphasis on diversifying 
            the economy away from dependence on oil gained further momentum 
            during 2008. Non oil exports grew by 27 per cent. Most of the real 
            estate development projects started in 2007 continued and the
            tourism sector grew by 22 per cent.<br>
            <br>
            <img border="0" src="cover90.jpg" align="right" hspace="8" vspace="6" width="279" height="310">Performance of western world financial markets continued to be 
            greatly affected due to the worldwide recession. MSCI AC World index 
            was down 43.5 per cent in 2008 as global equity markets finished 
            2008 down 43.5 per cent. Japan (-30.5 per cent) was the best 
            performing region followed by the USA (-38.5 per cent) and Europe 
            (-48.2 per cent). The worst performing regions were Global Emerging 
            Markets (-54.5 per cent) and Asia Pac ex-Japan (-53.3 per cent).<br>
            <br>
            Recessions are historically times when companies make the biggest 
            competitive strides or fall behind. A 2002 survey by McKinsey of the 
            performance of 1000 companies during an 18 year period found that 
            those that made the biggest leaps in profitability were often the 
            ones that increased their spending on acquisitions and innovation 
            the most amid recessions.<br>
            <br>
            During the year 2008, the revenues of Oman’s 20 largest companies 
            showed a handsome growth. Total revenues for the OER Top 20 went up 
            by 25 per cent to RO3,220mn. Corporate performance of the year 2008, 
            overall, was also slightly positive in spite of the global 
            recession. The profits for the year 2008 increased by four per cent 
            to RO480mn from the figure of RO462mn last year. The total market 
            cap of the OER Top 20 on December 31, 2008 was RO3,975mn, which was 
            down by 44 per cent compared to 2007. On April 2, 2009, the market 
            cap of the top twenty has once again gone down to RO3,281mn. The OER 
            Top 20 companies represent 68 per cent to the total market cap of 
            the MSM of RO5,829mn at the end of 2008. The average P/E ratio of 
            the OER Top 20 based on the profits of the year 2008 and the share 
            price on April 2, 2009 is 6.84 times earnings.</font></p>
