			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Interestingly, only one of the top five companies showing the best 
            return on equity employed comes from the banking sector. Four of the 
            companies in the top five are newcomers to the list. Shell steps up 
            to the number one spot from being No.2 last year; Omantel, Oman Oil, 
            Al Anwar and OMINVEST take the two to five positions knocking out 
            Oman Cables, AES Barka, OHI and Raysut Cement who appeared in this 
            table last year.<br>
            <br>
            Andrew Wood, Chairman of Shell Oman Marketing, in his report to the 
            shareholders states that overall the company remained the market 
            leader amongst oil marketing companies operating in Oman. Wood adds 
            that the company achieved unprecedented financial results recording 
            a historically highest ever net profit of RO12.5mn compared to 
            previous year’s net profit of RO11.06mn. Despite the uncertainty 
            surrounding the major economies and Oman’s economy, proactive 
            management as well as sound operational performance were the 
            principal drivers of the commendable financial performance in 2008. 
            Wood explains that the retail business remains the most important 
            segment for the company. Total retail volumes sold was in excess of 
            1.3bn litres. The aviation business saw over 50 per cent increase in 
            volumes in 2008. The market penetration in the lubricants market 
            also registered record level of sales volume.<br>
            <br>
            <img border="0" src="cover98.jpg" align="left" hspace="8" vspace="6" width="280" height="312">The board has recommended a final dividend of 115 per cent for the 
            year continuing the recent trend of year on year dividend growth. 
            Wood adds that the fuels market normally follows the economic trend 
            of the country and is thus expected to see some uncertainty due to 
            the volatility of the global oil prices. The Sultanate will not be 
            entirely immune from the current global recession and demand, 
            especially in the commercial and aviation sectors which, may be 
            affected as tourism and development projects slow down. The retail 
            segment on the other hand is not expected to be affected.<br>
            <br>
            Three of the top five earnings per share growth companies are 
            newcomers on this list. Oman Flour Mills jumps straight into number 
            one spot pushing Al Anwar down to the second position. Renaissance 
            comes in at the number three position, OHI continues to remain in 
            the number four position and Shell comes into the number five spot. 
            Al Jazeera Steel, OMINVEST and Al Maha Petroleum are all out.<br>
            <br>
            Samir Fancy, Chairman of Renaissance, in his report to the 
            shareholders states that Renaissance has achieved record results for 
            the seventh consecutive year. The consequence of continued focus on 
            values-driven operating model is, yet another year of growth and 
            record financial performance. Revenues of RO234mn are up from 
            RO199mn in 2007. Revenue increased by 17.6 per cent, operating 
            profit has increased by 35.8 per cent and operating margins improved 
            from 13.8 per cent in 2007 to 16 per cent in 2008. The net profit of 
            2008 includes a capital gain of RO4.8mn from the divestment of the 
            group’s former technology business.<br>
            <br>
            For the current fiscal year the board has recommended a cash 
            dividend of 10 per cent and a stock dividend of 15 per cent. Looking 
            ahead, Fancy states that it is clear that the prognosis for 
            Renaissance is good for continued growth in 2009, in spite of the 
            recession. As usual, performance will not be even across each 
            quarter. The first quarter of the year we have usual seasonal effect 
            of winter downtime in the northern Caspian and in Q1 and Q2, 2009 we 
            have a major programme of dry-docking that will ensure Q3 and Q4 
            will likely record the best financial performances of the year 
            ahead. Going into 2009, Fancy states that the company does not 
            underestimate the challenges of the recession but is taking all 
            prudent measures necessary to avert its dangers. He further states 
            that they have the right businesses, with the right customers, in 
            the right markets and have the right assets and above all the right 
            people.<br>
            <br>
            <b>Role reversal<br>
            <img border="0" src="cover99.jpg" align="right" hspace="9" vspace="6" width="281" height="349"></b>All of the five companies on the shareprice growth list are 
            newcomers with Oman Oil taking the top position. Shell Marketing 
            comes in second followed by Al Hassan Engineering, Omantel and Al 
            Maha Petroleum. Al Anwar Holdings, Renaissance Services, Oman 
            Cables, Bank Dhofar and Raysut Cement from last year have all been 
            knocked out.<br>
            <br>
            Salim Abdullah Al Rawas, Chairman of Oman Oil Marketing in his 
            report to the shareholders states that 2008 was another successful 
            year for the company. The total revenue increased by 11 per cent to 
            RO170mn from the previous year’s 152.7mn. The revenue growth was due 
            to improved sales recorded for most products as well as higher 
            product prices in line with the international prices. As a result 
            the company’s pre-tax profit rose by 12 per cent to RO7.2mn.<br>
            <br>
            The board of directors recommended a final dividend of 35 per cent 
            for the financial year 31 December 2008. Al Rawas states that the 
            outlook for the coming year appears to be positive with the demand 
            for petroleum products expected to grow in line with Oman’s 
            projected economic growth. He adds that the company has placed 
            itself in a strong position for the future. Nonetheless competition 
            in all business sectors particularly in the retail business will 
            continue to persist. With ever increasing competition in the market 
            and aggressive efforts by the competitors, the company will remain 
            focused on expanding its network of facilities, improving quality of 
            its products and services, optimising costs and developing its human 
            capital even further.<br>
            <br>
            Three new companies have entered the ranking of the best five 
            dividend yield companies. Oman Flour Mills, Al Hassan and Raysut 
            Cement are newcomers in number one, four and five slots. OHI jumps 
            up to number two from number five last year and OIB retains the 
            third position.<br>
            <br>
            Manal Mohammad Al Abdawani, Chairperson of Oman Flour Mills in her 
            report to the shareholders for the year ended June 30, 2008 states 
            that while there has been remarkable improvement in the demand for 
            their flour products, which increased by six per cent in the local 
            market compared to last year, the company’s fodder sales fell by 
            eight per cent compared to previous year because of the shortage of 
            fodder raw material. The total sales amounted to 324,000 metric tons 
            which covers 65 per cent of the flour market in the Sultanate.<br>
            <br>
            Al Abdawani adds that the company exerted relentless efforts to 
            improve its operating performance. The company achieved impressive 
            results for the year ended June 30, 2008. The net profit for the 
            year was RO7.5mn compared to RO4mn for the previous year an increase 
            of 87 per cent. The company along with its subsidiaries made a net 
            profit of RO8.1mn compared to RO4.2mn in the previous year.<br>
            <br>
            Based on the good results achieved, the board has recommended a 
            dividend of 25 per cent for the year. Al Abdawani explains that the 
            company is continuing to improve operating efficiency through 
            diversifying its investments in equipment like the ship un-loader 
            that increases the discharge capacity thereby reducing cost. The 
            company is also working to add other equipment in order to improve 
            overall production efficiency. The company has also established a 
            subsidiary ‘Bread House’ to market frozen bakery products and will 
            establish an industrial bakery, the commercial production of which 
            is expected to commence in 2009.</font></p>
