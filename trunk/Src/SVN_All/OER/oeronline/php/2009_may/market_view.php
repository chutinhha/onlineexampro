                    </font><b><font face="Verdana" size="4">Market Rally<br>
                </font></b><font face="Verdana" size="1"><b>A spurt in oil 
                prices and good quarter one 2009 results helped the MSM to break 
                a 10-month long bear run<br>
                </b><br>
                The MSM30 benchmark index rose 8.2 per cent for the month ended 
                15th April 2009, after nearly ten months of consecutive losses. 
                The rally was lead by the banking and industrial sector stocks. 
                Service sector index somewhat offset the significant gains 
                recorded by the other two sectoral indices. Market activity 
                improved as volumes increased by 59.3 per cent while average 
                turnover per day increased by 112 per cent for the same period, 
                to RO9.33mn. The market rose on the back of stable oil prices 
                and greater visibility in earnings of the listed companies. 
                Investor’s confidence seemed to have picked up given attractive 
                valuation of blue chip stocks. Market breath was favourable with 
                an advance decline ratio of 1:1. <br>
                <br>
                <b>Leading the charge<br>
                </b>The index heavy weights that lead the rally were ONIC 
                holding (104 per cent) Bank Muscat (+31.7 per cent) and Oman 
                Emirates (+37.89 per cent). In key corporate events, Bank Muscat 
                informed the market that it has sold approximately 81 per cent 
                of its stake in HDFC Bank, India. The pre-tax profit on the sale 
                of this stake is approximately RO39mn and will be reflected in 
                the first quarter results. Majan Glass announced that the 
                company has started commercial production of its fifth 
                production line.
