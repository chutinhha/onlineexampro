                <b>GCC markets rebound</b><br>
                The month ended April 15, 2009 was a positive month for the GCC 
                markets and the benchmark MSCI GCC index increased by 15.94 per 
                cent. The Saudi stock exchange was the top gainer and the index 
                increased by 25.60 per cent during the month. The banking and 
                petrochemical counters among other boarder indices were the top 
                gainers during the month. Rise in oil prices coupled with 
                encouraging announcements by petrochemical companies led to the 
                positive sentiment among investors. The Qatar and Abu Dhabi 
                exchanges too ended on a positive note with the respective 
                indices gaining 13.99 per cent and 13.66 per cent. The 
                industrial and banking counters were among the top gainers on 
                both the exchanges. The Kuwaiti stock exchange rose by 11.68 per 
                cent with the broader sectoral indices ending mixed. Banking 
                counters were among the top gainers on the exchange. The Emir 
                approved an injection of KWD4bn into the banking sector as 
                deposits, which should boost the lending capacity of banks.<br>
                <br>
                The Dubai Financial Market saw a rise of 10.66 per cent with the 
                construction and real estate counters ending up on the exchange. 
                The Bahrain Stock exchange was a slow mover among the rest of 
                the GCC markets and the general index ended up 5.18 per cent 
                during the month. Rising oil and commodity prices coupled with 
                various government initiatives to boost investors’ sentiments 
                was the prime reason for the surge in the GCC markets. Moreover, 
                investors created new positions in anticipation of strong 1Q09 
