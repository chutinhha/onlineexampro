                <br>
                <b>What happens during stress?</b><br>
                When one is stressed the sympathetic nervous system causes the 
                heartbeat and the breathing rate to increase, the blood vessels 
                to narrow, which restricts the flow of blood. This creates an 
                unseen havoc in our system making us feel tired. On the other 
                hand the parasympathetic nervous system brings the heart and the 
                breathing rate to slow down and the blood vessels to 
                dilate—improving the blood flow. Meditation strengthens our 
                parasympathetic nervous system. It controls the uneven flow of 
                blood and oxygenates the mind thereby making it more alert and 
                agile<br>
                <br>
                <b>What is meditation?</b><br>
                Meditation is learning to focus attention, remaining at the same 
                time, mindful of thoughts, feelings, sensations and to observe 
                them in a non-judgmental way. The regular practice results in a 
                calm and composed mind, which can relate lackadaisically to its 
                own flow of emotions and thoughts as well as to the others’ 
                without qualms.<br>
                <br>
                <b>Open attitude </b><br>
                During meditation one has to have an open attitude. This means 
                letting distractions visit you naturally without judging them. 
                When the practitioner’s attention digresses he gently brings it 
                back to focus. Regular practice empowers a person with mental 
                balance and inculcates benign thinking.<br>
                <br>
                <b>Types of meditation</b><br>
                There are many types of meditations such as mantra meditation, 
                relaxation response, mind full meditation and Zen Buddhist 
                Meditation. Mindful Meditation comes to us from Buddhism. The 
                transcendental Meditation technique is a legacy of the Hindu 
                tradition. It uses a mantra (a word, sound or phrase repeated 
                silently) to prevent distracting thoughts from entering the 
                mind.</font></p>
