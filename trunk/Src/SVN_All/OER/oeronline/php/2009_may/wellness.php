                </b><br>
                The value of meditation to mitigate day-to-day stress related 
                pains and pressures is doubtless. Today most of the medical 
                community is prescribing meditation as an easy and risk free 
                alternative way to get rid of blood pressure, diabeties, and 
                coronary ailments. Not only this but meditation also relieves 
                asthma, insomnia, and numerous nervous disorders born due to 
