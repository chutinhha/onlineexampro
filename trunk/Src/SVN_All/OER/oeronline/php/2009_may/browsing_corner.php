                    </font><b><font face="Verdana" size="4">Inspiring Success 
                From The Top Down</font></b><font face="Verdana" size="1"><br>
                <b>In her book, “The Visionary Leader – How to inspire success 
                from the top down” leadership coach and consultant Susan Bagyura 
                gives useful tips for leaders on how to inspire success in their 
                team members, writes Raj Shekhar Chandola</b><br>
                <br>
                <img border="0" src="bc.jpg" align="left" hspace="8" vspace="6" width="200" height="461">Susan Bagyura has over 25 years of 
                sales, marketing and entrepreneurial experience. Having 
                experienced corporate America and having lived and worked in 
                Zambia, New Zealand, Ireland, England and Austria, she 
                represents a viewpoint that covers several perspectives. And it 
                comes through forcefully and clearly in this book.<br>
                <br>
                The world today is changing rapidly and if someone is doing 
                business the way they were five years ago, they are obviously 
                not doing it the right way. In her book Bagyura provides 
                op-point, insightful, inspiring and actionable tips on how to 
                grow and what it takes to lead in today’s times. Susan points 
                out that thought affect attitude and attitude affects behaviour. 
                Thus the organisation becomes a refection of its leadership and 
                direction it provides to the organisation. Fear can hinder 
                success and therefore the leader’s approach makes a difference 
                in the bottom line results.<br>
                <br>
                <b>Team player</b><br>
                Bagyura also points out that mindsets affect the level of 
                success so the leader should know his leadership style, whether 
                it is authoritarian, democratic or delegating. She says that the 
                best leader flex between all three. She says team building 
                requires several basic skills including listening, questioning, 
                persuading, sharing and participating. She advises that we 
                should learn from our mistakes and move on. Bagyura reminds that 
                there is a difference between goal-setting and goal achievement 
                and advises we aim high and set goals that reach beyond regular 
                performance even suggesting the goals leader set should either 
                excite or scare him. Bagyura advises not to settle for limited 
                expectations because it will only create limited results and not 
                to get too hung up on the “how” of achieving the goal. Her 
                advise is to set BIG goals, stay focused and believe you will 
                reach the goal.<br>
                <br>
                <b>Action orientation</b><br>
                Lastly, Bagyura points out that indecision results in 
                disintegration so the leader should not make excuses and take 
                action; be willing to take intelligent risks; should remember 
                that resources and circumstances should not limit the outcome; 
                use fear as a motivator rather than letting it paralsze 
                progress; and remember, once a decision is taken, conditions 
                change. Bagyura’s book is loaded with valuable concepts and 
                practical applications. It provides a roadmap for leaders who 
                want to be more inspirational and is a must read for all who 
                manage other people. It shows how inspiring others is the way 
                forward to top performance and becoming a strong leader.</font></p></td>
