                <b>Increase the life and improve the efficiency of your 
                air-conditioner with these few handy tips. Also see how you can 
                make you A/C more energy efficient so that it doesn’t run up 
                huge electricity bills each month</b><br>
                <br>
                <img border="0" src="spotlight1.jpg" align="left" hspace="8" vspace="6" width="300" height="98">The 
                maintenance of an AC is an area that most consumers and 
                commercial clients need to learn about. It is not always 
                necessary to order dealer service when a little bit of care and 
                maintenance on your part will go a long way in improving the 
                working life of your air conditioner<br>
                <br>
                Simple things such as regular cleaning of the filters, helps 
                increase the life of your AC. However, it is more prudent for 
                you to enter into an annual maintenance contract with the 
                authorised service centre of your brand of AC, so that the units 
                are maintained at a nominal cost with little hassle. The 
                following few tips will help one maintain an air-conditioner in 
                an easy simple way:<br>
                <br>
                Understand that an air conditioner, apart from cooling, also 
                conditions the air inside your room like humidity, dust, order, 
                fungus, bacteria, etc. All good quality ACs have cooling, 
                humidity control, dust removal, anti fungal and anti-bacterial 
                functions and these need to be used properly.<br>
                <br>
                Split ACs are now affordable, very quiet and energy saving as 
                compared to window ACs. They are also more hygienic and prevent 
                birds from making their nests in air gaps, rodents creeping in 
                and air from leaking.<br>
                <br>
                Clean the filter of your AC once a fortnight, or when the 
                indicator lamp on the front panel lights up. Dirty air filters 
                force your system to work harder to push cool air through your 
                home. This uses more energy and places extra strain on the air 
                conditioning system. Take the filter(s) out and hold it to the 
                light. If the dust on the filter is so think that you can’t see 
                much light shining through the filter, it’s time for new 
                filters. A clean air filter, unobstructed by the dirt and other 
                debris, will save you money on energy costs and prolong the life 
                of your air conditioner. If you do not know where your air 
                filters are or how to replace them, ask your HVAC technician to 
                show you during an inspection.<br>
                <br>
                Keep the doors of your rooms open for a few minutes every day to 
                allow some fresh air to enter. Do not spray any type of 
                deodorant or perfume into your split ACs. Doing so will result 
                in foul smell, necessitating a cleaning of the indoor unit.<br>
                <br>
                Avoid smoking in an AC room as indirect smoke can harm other 
                family members and it is very difficult to remove the odour even 
                by servicing.<br>
                <br>
                For safety, always turn the thermostat and outdoor unit’s 
                breaker off before doing any work around that outdoor unit. The 
                coil can be cleaned using a soft-bristle brush to gently sweep 
                the fins. Always brush in line with the fins, and be gentle 
                because the fins can bend easily. Because the fan pulls air 
                through these fins, you can expect to find dust clinging to the 
                fins. Removing this dust and other debris will reduce resistance 
                and increase efficiency.<br>
                <br>
                If you smell bad odour, it normally will not be from your AC but 
                from your carpet, curtains, linen, excessive sweat, etc. 
                However, if the filter is not cleaned routinely or the drainage 
                is clogged and water remains in the drain pan for long, it can 
                cause foul smell. Here, it is essential that the drainpipe be 
                connected properly to prevent foul smell from entering through 
                the pipe.<br>
                <br>
                Keep the exhaust fan of your bathrooms ON when you are at home.<br>
                <br>
                Keep the split AC ON even when you are out of home at a 
                temperature of 27 degree C. This will keep your home cool and 
                healthy, especially when the outside temperature is above 42 
                degree C, with high humidity. A good quality AC is meant to be 
                ON 24 hours a day.<br>
                <br>
                In Oman today, an AC is needed for not more than seven months a 
                year. Using the AC when indoor temperature is less than 25 
                degree C is a waste of energy. Between November and March, do 
                not switch ON the AC in fan mode, instead use a ceiling fan that 
                will give you comfortable and natural sleep.<br>
                <br>
                When the outside temperature goes above 42 degree C, keep the 
                bedroom and living room ACs ON at a temperature setting of 27 
                degree C even if you are out of the room. This will prevent the 
                room walls and roof from becoming excessively hot. Hot walls and 
                roof lead to continuous running of the AC’s compressor, 
                resulting in excessive power consumption and inefficient 
                cooling. When you re-enter the room, you can adjust the 
                temperature settings. Your electricity bills will not be high as 
                the compressor will shut down on reaching the set temperature. 
                At such times, the AC can be turned off after 10 PM as the room 
                temperature will remain at around 25 degree C and a ceiling fan 
                running at slow speed will give you comfortable sleep. <br>
                <br>
                If you own a villa, plant trees around the building, as these 
                can help you save on your electricity bills.<br>
                <br>
                Insulate your rooftop professionally and use good quality 
                insulated bricks for the walls. Use thick curtains so that the 
                heat rays do not enter the house / flat. Keep the outdoor unit 
                of your split air conditioner in a shaded area or place it in 
                such a way that afternoon sunlight does not hit it directly. <br>
                <br>
                Get the AC checked and serviced by your distributor once a year 
                for any gas leak and get the outdoor unit washed using high 
                pressure water. This will help you save on your electricity 
                bills. Cleaning the filter every fortnight is very important to 
                save energy. Users themselves can do filter cleaning and use 
                service centres for other routine services.<br>
