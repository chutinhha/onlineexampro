                <b>You may have heard of terms like BTU, tonne, etc., when you 
                talk about ACs, but do not know what they mean? Get to know 
                industry jargon that will help you select an AC or refrigeration 
                product that best meets your needs<br>
                </b><br>
                <b>What size air conditioner do I need?<br>
                </b>The cooling capacity of an air conditioner is measured in 
                British Thermal Units (BTU) and the capacity you require depends 
                mostly on the size of the room you need to cool. If you buy an 
                air conditioner that is too small, it will not cool adequately. 
                If you buy one that is too large, the air conditioner will cool 
                the room before it dehumidifies and this can leave you feeling 
                clammy. For larger areas, sometimes, two smaller units are more 
                efficient than one large unit. Your dealer will have additional 
                details.<br>
                <br>
                <b>How often should I clean the filter?<br>
                </b>To ensure maximum cooling capacity and efficiency, you 
                should keep the filter clean. During peak usage times, the 
                filter should be inspected on a weekly basis to make sure that 
                it is not clogged with dust. <br>
                <br>
                <b>What does EER mean?</b><br>
                The EER, or Energy-Efficiency Rating, measures an air 
                conditioner’s energy consumption and efficiency. A higher EER 
                rating means that your AC is consuming less energy.<br>
                <br>
                <b>What is the tonnage in ACs?</b><br>
                The unit of measure used in air conditioning to describe the 
                cooling capacity of a system. One ton of cooling is based on the 
                amount of heat needed to melt one ton (2000 lbs) of ice in a 24 
                hour period. One ton of cooling is equal to 12,000 Btu/hr.<br>
