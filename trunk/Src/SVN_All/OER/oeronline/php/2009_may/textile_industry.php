		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>Turnaround Mode</b></font><font face="Verdana" size="1"><br>
                <b>After three continuous years of losses, the Sultanate’s only 
                textile mill ‘Oman Textile Holding’ is on the road to recovery 
                under a new leadership. Akshay Bhatnagar reports</b></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
                <img border="0" src="tex.jpg" width="475" height="274"><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>
                <br>
                </b>When the whole world is reeling under the pressure of 
                economic slowdown, Rusayl based Oman Textile Holding is quietly 
                working towards making a successful comeback. It is not an easy 
                task for the company that has reported a net loss for the last 
                three financial years and accumulated losses of RO1.79mn by the 
                end of 2008. Despite the quota regime in place and having the 
                first mover advantage, the company couldn’t get its act together 
                on a sustainable basis in the past. So what gives it the 
                confidence that the current spark will not be a flash in the 
                pan?<br>
                <br>
                <b>Road to recovery<br>
                </b>Last year Yousuff Ahmed, a well qualified professional with 
                well-rounded global experience in the textile sector, was 
                brought on board as the new chief executive officer. In the last 
                one year, he has managed to reduce the bleeding. The net loss 
                has been brought down to RO671,450 in 2008 compared to a high of 
                RO966,599 in 2007. <br>
                <br>
                “We have improved our yield per worker and machine, increased 
                unit value realisation. The product mix has been rationalised, 
                product lines trimmed and focused on markets and segments such 
                as special supplies business. We are now getting our supplies on 
                credit instead of cash basis from almost the same vendors. To a 
                large extent, this has taken care of our working capital 
                requirement,” informs Ahmed. <br>
                <br>
                <img border="0" src="tex1.jpg" align="left" hspace="8" vspace="6" width="181" height="388">The company imports raw material from markets including India, 
                Indonesia and certain parts of Europe. It sells fabrics in the 
                GCC region with 30 per cent of the business coming from Oman. 
                Its fabrics are consumed mainly by institutions such as defence 
                forces and manufacturing industries. The company has obtained 
                approval from SGS, the UK based global quality lab, for the 
                specifications mainly catering to the institutional market 
                segment. <br>
                <br>
                <b>Moving forward<br>
                </b>The CEO of Oman Textile Holding is a picture of complete 
                confidence when it comes to the future of the company. The 
                company has an annual production capacity of 20 million meters 
                of fabric but currently its capacity utilization is just 40-50 
                per cent. The global industry average is around 90 per cent. “We 
                plan to increase our capacity utilisation to 60-70 per cent by 
                the end of this year. In 2010, we aim to achieve 95-100 per cent 
                mark,” says the CEO. But how? “It is not very difficult. The key 
                is to upscale our operations and bring in economies of scale. 
                The market in the GCC is huge. Look at the requirements from 
                defence forces segment alone. We are looking at 40-50 per cent 
                offtake of our fabrics from this segment alone. The demand for 
                Arab clothing such as dishdashas and female wear is extremely 
                high. Currently, the demand in this segment is met by imports 
                from rest of the world,” informs Ahmed.<br>
                <br>
                The implementation of Oman-US free trade agreement (FTA) has 
                also opened a vast new business opportunity for the company. 
                “The FTA has provided relaxation for five years in the ‘Rule of 
                Origin’ in the case of textiles imported from Oman. Look at 
                developing nations such as Sri Lanka and Bangladesh. They have 
                emerged as a major force in the textile industry in the world. 
                Oman has a much superior infrastructure and development level, 
                and logistics advantage due to its proximity to US (and Europe 
                too). I see no reason why we can’t penetrate into US and Europe 
                markets,” says Ahmed.<br>
                <br>
                <img border="0" src="tex2.jpg" align="right" hspace="8" vspace="6" width="188" height="186"><b>Challenges ahead<br>
                </b>Oman Textile Holding’s objective to bounce back is not 
                impossible but Ahmed has a difficult task on his hand. Many 
                textile companies from the Indian subcontinent are looking at 
                setting up base in Oman to take advantage of the Oman-US FTA. 
                With stronger financial muscle, industry networking, latest 
                machinery and assured supply of raw material, they can hit the 
                prospects of OTH severely. “We are aware of the impending 
                competition but we are on a strong footing as we have a very 
                well established base in Oman and the market in US is big enough 
                to accommodate many companies like OTH. But we need to be 
                focused on upscaling our operations to optimise on the 
                opportunity,” replies Ahmed.<br>
                <br>
                The company may find it difficult to raise the funds for the 
                expansion, if needed, as the banks in Oman have become more 
                stringent in granting loans when it comes to companies with not 
                an impressive track record. Ahmed says that, if required, 
                funding will not be an issue for them. <br>
                <br>
                With the global economy expected to register a negative growth 
                this year and prevailing intense competition in the textile 
                industry, the going is not expected to be easy for the sole 
                Omani textile mill. The product prices are moving southwards, 
                margins are getting squeezed and large manufacturers from Asian 
                countries facing the dilemma of excess capacity; OTH will have a 
                tough task to further improve its bottom line in 2009. <br>
                <br>
                It will be interesting to see how the company’s strategy to 
                introduce superior quality, new and contemporary products, brand 
                loyalty, greater penetration in more profitable segments such as 
                special supply business is going to result in expected growth. 
                So far the company has performed reasonably well in 2008 to give 
                the confidence that it is on the right track but the road to 
                recovery is long and circumstances adverse.</font><p align="right" style="margin: 3px 10px">
