		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Creating Innovative 
                Solutions</font></b><font face="Verdana" size="1"><br>
                <b>Jose Lora, CEO, Juthoor Development Services sees its 
                comprehensive development services like project feasibility, 
                implementation and develo pment control as being key 
                differentiators in the market. Mayank Singh reports</b><br>
                <br>
                <img border="0" src="services.jpg" align="left" hspace="8" vspace="6" width="300" height="435">The real estate sector has been in the news globally for all the 
                wrong reasons. What started as a mortgage problem (the infamous 
                sub prime) snowballed into a financial crisis of historical 
                proportions. While, the GCC real estate market has not witnessed 
                anything similar – with the exception of Dubai, there have been 
                projects that have run into problems for a number of reasons. 
                Say Jose Lora, CEO and Development Director, Juthoor Development 
                Services, “A number of projects are in trouble because the 
                management of these projects are not good.”<br>
                <br>
                Juthoor, a recent entrant to the market tries to address this 
                problem. The company provides turnkey solution to its clients. 
                “Juthoor was formed because there was a specific need for 
                development services – we take a project from the stage when it 
                is an idea and see whether it make financial sense. Once a 
                client decides to go ahead with the project we then get through 
                the feasibility part, get the contractors, asset managers, 
                engineers, planners, bankers, sales and marketing people making 
                the project profitable,” says Lora. <br>
                <br>
                <b>Tentative steps</b><br>
                Juthoor is an arm of REO or Real Estate Opportunities an 
                organisation that was set up to look at the needs of the Oman 
                market and then to start building projects to suit these 
                demands. The company is looking at introducing other services 
                like broking, construction etc. “We have to take a decision 
                about either bringing an existing brand or developing our own 
                brand for these related services.” Naysayers may argue that 
                floating numerous companies dilutes a brand but Lora dismisses 
                such speculation, “The needs in terms of real estate in Oman are 
                global and so we are developing service companies that will make 
                us stronger. They will also have the opportunity to act on their 
                own and become viable companies.” <br>
                Though based in Oman, Juthoor is looking at the GCC region for 
                projects. It is presently evaluating a few projects in Abu Dhabi 
                and Saudi Arabia apart from the Sultanate. “Juthoor has just 
                been launched so we need to wait and see what the future brings. 
                For every ten projects that we look at, we recommend only a 
                couple. So we spend a lot of time looking at projects across the 
                GCC, but they will not tie up all our time.” <br>
                Catering to local needs<br>
                <br>
                The strength of Oman’s market in terms of real estate 
                development is that it is still a nascent market. This has 
                shielded it from the financial storm that what other developed 
                markets have weathered. Says Lora, “Oman is in an enviable 
                situation, wherein there are not many large projects except The 
                Wave. When you look at Oman it is really at the bottom, so there 
                is no where that it can go but up and the opportunities are 
                immense. We see this as an opportunity rather than a drawback.”
                <br>
                <br>
                The Sultanate has a number of factors fuelling the market. The 
                demographics of the country being the foremost among them – over 
                20 per cent of Oman’s population is between the age of 20-30. 
                “This means that they need to get serious about owning a house. 
                There is virtually no housing available for the middle and lower 
                income sections of the population.” Second, Oman is encouraging 
                industrial activity which raises the need for office space. 
                Presently, there is a shortage of good office space. Lora feels 
                that rentals are high for office space that is not classy and 
                this is the reason why a number of businesses are operating out 
                of villas. Third, the retail end of the market holds a lot of 
                potential as fashion centres and entertainment complexes 
                mushroom. “There is a great opportunity for shopping centres, 
                hotels and retail space.” Lastly, the country needs mega 
                distribution and logistics centres. <br>
                <br>
                In geographical terms Oman is a gold mine. It is considered to 
                be the most beautiful country in the GCC, with a strong culture 
                and hospitable people. Salalah with its hospitable climate, is a 
                big draw for the moneyed classes. “There is a need to let the 
                world experience Oman and there is sure to be great demand.” <br>
                <br>
                <img border="0" src="services1.jpg" align="right" hspace="8" vspace="6" width="192" height="187">Till now, Dubai has been on everyone’s radar as the emirate with 
                its biggest, tallest, most luxurious and iconic buildings 
                attracted widespread attention and gave developers the biggest 
                profit margins. The financial crisis though has led to a rethink 
                on the realities of the real estate market. “Oman needs 
                affordable residential houses for the middle income strata. The 
                reality is that residential projects like The Yiti are not meant 
                for the average Omani, who does not have enough choices and it 
                is just a matter of time when they start asking what about us,” 
                says Lora. This calls for a detailed analysis of land prices, 
                construction costs and creation of affordable houses for these 
                demographic groups. “It is going to take a lot of thought to 
                make that happen,” says Lora. <br>
                <br>
                <b>Global crisis</b><br>
                Looking at the global crisis, Lora says, “We as developers look 
                upto the government and financial institutions – looking back 
                greed overpowered the financial institutions and there was a 
                lack of control on the part of the government and this affected 
                the world. The financial markets in the US have had the need for 
                better returns and profits and the government did not focus on 
                what needed to be done before it was too late. The end consumer 
                is not free of culpability because there was a lack of saving 
                and overstretching their financial abilities.”<br>
                <br>
                Despite the crisis, the GCC region is better placed than most 
                parts of the world. While the US and Europe expect to post 
                negative growth, the GCC countries are looking at posting 
                positive numbers. Oman has projected a GDP growth of over 2.5 
                per cent in 2009. Says Lora, “We sometimes concentrate on one 
                issue and fail to look at the total picture, the price of oil 
                has gone down from over $140 to under $50 a barrel, the reality 
                is that value of all the commodities that Oman imports has gone 
                down drastically, so we are not as badly hit as some countries 
                in the western world.”<br>
                <br>
                Lora has worked extensively in the US, Mexico, South America and 
                in the GCC. In the US he was the design and construction head 
                for the entire East Coast at Homart Development which at one 
                time was the third largest multi-use and shopping centre 
                developer in North America. He then joined Sears Organisation 
                and moved to Mexico to head the company’s Latin America 
                operations. At Sears, he was instrumental in expanding the 
                business in Latin America. His real estate consultancy firm 
                called Arlo grew into one of the most prominent real estate 
                consultancies in the region. He has also managed projects for 
                the Hyatt chain. In all, Lora boasts of over 30 years of real 
                estate experience. Having studied architecture at the University 
                of Illinois he went onto read real estate investment and finance 
                at Harvard University. “I am one of those rare characters who 
                understands the design process, construction process and the 
                business side of development,” says Lora. Juthoor has chalked 
                out an impressive blue print, now it needs to walk the talk.</font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
