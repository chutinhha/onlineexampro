            <b>
            <font face="Verdana, Arial, Helvetica, sans-serif" size="2" color="#CC3333">
            Feature<br>
            </font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            Since the Fortune 500 rankings started in 1955, only 71 companies 
            have survived on the list till date, 2000 companies having come and 
            gone off the ranking. How to build an enduring company is the 
            biggest challenge facing CEOs across the globe. We pose the question 
            to six CEOs of Omani companies</font></b></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <a name="Keeping">Keeping the legacy</a></font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>‘Maintaining a heritage calls for concerted effort’ – Adil Ghouse, 
            CEO, Consumer Products Group, Khimji Ramdas</b><br>
            <br>
            <img border="0" src="cover12.jpg" align="left" hspace="8" vspace="6" width="275" height="271">There are several fundamental elements that make for a successful 
            company,” says Adil Ghouse, CEO, Consumer Products Group, Khimji 
            Ramdas (KR). Managing ones finances is the first pre-requisite of 
            any successful company. Financial discipline is important, as 
            successful businesses are based on profits. <br>
            <br>
            Sustainability of a business is another key factor – this may 
            require changing products or variants to suit the changing dynamics 
            of the market and customer tastes. Says Ghouse, “You cannot deal 
            with new issues in old ways in a competitive environment.” KR, a 
            company that dates back to 1830 is itself a living testament of 
            adapting to change. For instance, the group has in the last few 
            years repositioned Khimji Mart (K-Marts) as a neighbourhood store, 
            to differentiate it from the large format stores. The other part of 
            the strategy has been to focus on towns outside the Capital area, 
            like Bahla, Sur, Nizwa etc. In the last six years the number of 
            K-Marts have gone up from four to 16. The quality of service and 
            product profile of these stores have made them destination markets 
            in most of these smaller towns. To cite another example, over the 
            last five years, KR has gone from distributing 500 stock keeping 
            units (SKUs) from P&amp;G to 1500 SKUs. This has necessitated a change 
            in the process systems followed by the company. “There are several 
            elements involved in managing the supply chain like the accuracy of 
            forecasting, technology, collecting and analysing the data and 
            communication channels with the principals,” says Ghouse.<br>
            <br>
            <b>People make the difference<br>
            <img border="0" src="cover11.jpg" align="right" hspace="8" vspace="6" width="222" height="127"></b>The importance of people can hardly be overstated, as it is their 
            efforts and passion that are the bedrock of any institution. 
            Instilling the right values and guiding them is a pre requisite for 
            going from good to great. KR follows a stringent selection process 
            to ensure that it is recruiting the right talent. Recruits are then 
            put through an induction programme at the Khimji Training Institute, 
            which is followed by an on-the job training. The company also 
            carries out regular ‘People Development Programmes.’ Apart from 
            skill sets, these programmes are geared to instil the KR values of 
            Trust, Care and Commitment amongst its people. “One of the elements 
            of having a 138 year old heritage is that our stakeholders have a 
            lot of faith in the KR name, this puts on us the responsibility of 
            ensuring that this heritage is not compromised at any given point,” 
            avers Ghouse.</font></p>
            <hr color="#CCCCCC" width="90%" size="1"> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <br>
            </font>
            <b><font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <a name="Basic">Basic instinct</a><br>
            </font></b>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>‘Start focusing on your priorities’ – Richard P Russell, MD &amp; CEO, 
            Blue City Company 1<br>
            </b>
            <br>
            <img border="0" src="cover9.jpg" align="left" hspace="8" vspace="6" width="275" height="228">Going back to the basics namely our core values helps a lot during a 
            downturn as we can deliver on these same set of values to the 
            customers,” says Richard P Russell, MD &amp; CEO, Blue City Company 1, 
            which oversees the Phase I of Al Madina A’Zarqa. “When the going 
            gets tough, customer focus is important because this is where our 
            core business is as we are a real estate company,” adds Russell. 
            Different companies adopt different strategies in a downturn. There 
            are many companies, which rise fast, and then they ‘unexpand’ 
            rapidly. Blue City is in a better position because it has been going 
            through a steady expansion and on the other side, the company has 
            been filling its organisational chart with professionals.<br>
            <br>
            <b>Focus on priorities</b><br>
            Thus, as a first step in a downturn, you start focusing on your 
            priorities and look at the value added propositions that you can 
            make for your customer. Then you need to cut out all the unnecessary 
            action and it may vary from company to company. The most important 
            is the efficient use of your manpower. Sometimes it is cost 
            effective to outsource your jobs while at other times, the proper 
            utilisation of manpower and talent is extremely important.<br>
            <br>
            <b>
            <img border="0" src="cover10.jpg" align="right" hspace="8" vspace="6" width="194" height="207">The Omani example<br>
            </b>Russell points out that we don’t need to go far to look for examples 
            as Oman itself is a perfect analogy on how to beat the downturn. 
            Oman, under the wise leadership of His Majesty The Sultan, is like a 
            large well-managed company. While many other neighbouring countries 
            are reeling under the impact of the financial slowdown, Oman is well 
            positioned to weather the economic storm as its policies of prudent 
            spending, conservative outlook and sound financial regulations have 
            stood the country in good stead. If you add up the short term 
            five-year policies and the long term ‘Vision 2020 development 
            policy’ crafted by the government, lo and behold, you have the 
            perfect ingredients to last any kind of financial crisis, Russell 
            opines.<br>
            <br>
            A company also has to make sure that its foundations are right 
            because form follows function and not the other way round. Again 
            this is going back to the basics. A downturn is actually a catalyst 
            for expansion into new horizons and brings out new ideas and 
            innovations.</font></p>
            <hr color="#CCCCCC" width="90%" size="1"> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <br>
            </font>
            <b><font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <a name="Close">Close to your knitting</a></font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>‘One of the fundamental philosophies of our group is to be 
            conservative’ <br>
            – P Chandrasekhr, Group General Manager, Jawad Sultan Group<br>
            </b>
            <br>
            <img border="0" src="cover7.jpg" align="left" hspace="8" vspace="6" width="275" height="329">There are three or four significant aspects of building successful 
            companies,” says P Chandrasekhr, group general manager, Jawad Sultan 
            Group of Companies (JSG), he adds, “CEO’s need to stay focussed on 
            their strategy whether it is a period of buoyancy or crisis, one 
            should not let emotions run riot.” A stable senior management team 
            helps in building an enduring company, by providing leadership, 
            vision and keeping in view shareholder objectives. <br>
            <br>
            <b>The purse strings <br>
            </b>Managing finances and maintaining cash flow is important for any 
            company. “One of the fundamental philosophies of our group is to be 
            conservative. We do not want to diversify into unrelated areas by 
            leveraging. Mostly, we try to use our profits to expand the 
            business.” The group is thus less dependent on external funding. A 
            look at JSG’s core businesses underscores the point. Capital Store, 
            their flagship retail brand has been around for 30 years. Counting 
            shop-in-shops there are ten Capital Store outlets in Oman. Its 
            telecom business (Jawad Sultan Technologies) has migrated from 
            selling products to providing technology solutions. In the 
            entertainment business, the company owns a number of large screen 
            cinema halls under the City Cinema brand name. The group recently 
            forayed into Sohar opening an entertainment complex. Plans are afoot 
            to expand the 
            <img border="0" src="cover8.jpg" align="right" hspace="8" vspace="6" width="196" height="114">business to Salalah and Buraimi. As a policy the group 
            has focussed on businesses that generate a daily cash flow. <br>
            <br>
            Being in the same business for years gives a manager domain 
            knowledge. This comes in handy while taking a decision. Says 
            Chandrasekhr, “After a point all decisions are taken on gut feel or 
            by the rule of thumb and detailing and factual
            information can take you only so far.”<br>
            <br>
            <b>Devil in the details </b> <br>
            Compensations should be steady and evenly distributed, so that 
            salary structures are seen as being fair. Attention to detail is 
            important as it sends a signal that the CEO has his hands ‘on the 
            pulse’ of the company. The unique feature of businesses in the Gulf 
            is that even in listed companies, the family culture is dominant. So 
            though, there are professionals, the patriarch runs the show in most 
            cases. “Decisions are taken by consensus between family members with 
            the advice of professional mangers. It is a good business model 
            because it has endured over time,” says Chandrasekhr.
            </font>
            </p>
            <hr color="#CCCCCC" width="90%" size="1"> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <a name="People">People Power</a><br>
            </font></b>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>‘It is only the people-focus that will keep a telecom company afloat 
            whether it is a boom or bust time’, – Ross Cormack,
            CEO of Nawras</b><br>
            <br>
            <img border="0" src="cover5.jpg" align="left" hspace="8" vspace="6" width="275" height="201">The telecommunications business is not majorly affected by a 
            downturn, nevertheless new strategies are chalked out because people 
            need to communicate with each other. Therefore, living out the 
            customer experience is the most important thing for a company like 
            Nawras as it is the customer who gives the reward, says Ross Cormack, 
            CEO of Nawras.<br>
            <br>
            “Actually, our industry can be the contributor for turnaround during 
            the difficult times like these because we are actually growing. We 
            would like to hire 100-200 new people for direct employment in our 
            company. Further, our point of sales and distribution network is 
            being expanded to nearly 1000 by the end of 2009. Thus we are also 
            generating thousands of indirect employment,” Cormack says. Nawras 
            follows its motto of being ‘pleasingly different’ whether it is a 
            downturn or not, says Cormack. An important thing is the quality of 
            service that we provide which is again determined by the quality of 
            people that we employ as our staff. Therefore Nawras is all about 
            people. Therefore the selection of people is very important because 
            a company should have capable employees who know the technical 
            requirements and most importantly these employees should be able to 
            live the customer experience, says Cormack.<br>
            <br>
            <b>Innovative style of working</b><br>
            <img border="0" src="cover6.jpg" align="right" hspace="8" vspace="6" width="194" height="157">Another factor which, differentiates companies like Nawras, is the 
            style of working, points out Cormack. “We have an open and inclusive 
            style of working and this helps in making decisions quicker. You can 
            share information as and when you require it. A working environment 
            should have the element of fun and managers should not spend their 
            time minding their subordinates but they should manage the people in 
            such a way as to create motivation. This is one of the key elements 
            of our success,” he says.<br>
            <br>
            Nawras also encourages team-work vigorously and people are asked to 
            form virtual teams to help them solve the customer’s problems. The 
            fourth but not the least component of the success of Cormack’s 
            company is that it is a people based company and as such those who 
            commit mistakes are not discouraged but on the contrary they are 
            motivated to do better. They are encouraged to quickly fix their 
            mistakes and learn as a company. By doing this, they create a 
            climate of erasing the guilt and build an ever greater quality of 
            service.
            </font>
            </p>
            <hr color="#CCCCCC" width="90%" size="1"> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <b><font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <a name="Read">Read the writing on the wall</a></font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b><br>
            ‘A CEO has to be futuristic in his outlook and should not ignore 
            modernisation and innovation’ – Krishna Kumar Gupta, CEO of Al Anwar 
            Holdings</b><br>
            <br>
            <img border="0" src="cover3.jpg" align="left" width="275" height="229" hspace="8" vspace="6">Metal Box India, was the market leader of tin packaging in India 
            around 50 years ago. But the company never thought that it would 
            fold up and eventually vanish from the market. In the face of stiff 
            competition from modern packaging methods and alternative packing 
            materials like plastic, Modern Box India lost out and today it’s no 
            longer remembered.<br>
            <br>
            <b>Outdated thinking</b><br>
            “The problem with companies like Modern Box India is that they don’t 
            see the writing on the wall. Similar is the case with General Motors 
            of the US, which was thought of to be ‘the company’ that could 
            outlast any situation. GM went on building bigger cars when the 
            price of gasoline was shooting up. Then what happened?,” asks 
            Krishna Kumar Gupta, CEO of Al Anwar Holdings. Gupta cites these 
            examples to highlight the importance of modernisation and innovation 
            in order to build an enduring company during a downturn like the 
            present global financial crisis. Although no one can predict the 
            future, it is important to look at the future and make the necessary 
            adjustments now. The Abu Dhabi government, for example, has abundant 
            reserves of oil but they are still investing heavily in renewable 
            sources of energy although it is not a preferred source of energy at 
            the moment, points out Gupta.<br>
            <br>
            <b>Evolve yourself</b><br>
            <img border="0" src="cover4.jpg" align="right" hspace="8" vspace="6" width="197" height="181">“A CEO of an efficient operation has to be futuristic all the time 
            even though his business is doing well because you never know what 
            the next decade holds for you. Never ignore innovation, new 
            techniques, new alternatives to keep up the development process 
            going on. Be a leader in all the departments mentioned above,” 
            advises Gupta.<br>
            <br>
            Situations like the present global downturn, segregates the men from 
            the boys because those who can do successful risk mitigation in the 
            present circumstances are those who will emerge the strongest. Risk 
            management is important because there is always risk in every aspect 
            of life, whether it is financial, political, for a country or an 
            individual. Therefore, identifying the risks and knowing how to 
            mitigate it is very essential and if you do it carefully, you cannot 
            fail, Gupta says.<br>
            Actually, any problem also presents an opportunity and the challenge 
            for CEOs is to convince the board of directors that ‘here is the 
            problem and here is the opportunity’. If any manager can achieve 
            that it opens the door to build an enduring company come what may.</font></p>
            <hr color="#CCCCCC" width="90%" size="1"> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            <b><a name="Future">Future Ready</a></b><br>
            </font>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>‘Our 40 year legacy inspires us’ – Giles Cunningham, CEO, Zubair 
            Corporation</b><br>
            <br>
            <img border="0" src="cover1.jpg" align="left" hspace="8" vspace="6" width="275" height="298">How to build enduring companies? If we had certain answers to the 
            question we would be wanted by every company in the world,” says 
            Giles Cunningham, CEO, The Zubair Corporation. On a more serious 
            note he thinks it is about espousing certain values – the paramount 
            amongst these being customer centricity – which is an ability to 
            understand the changing needs of customers overtime. There are 
            numerous instances of how successful companies went down only 
            because they were not able to keep up with the times. <br>
            <br>
            <b>A new sensibility</b><br>
            Keeping up with the times requires an entrepreneurial streak of 
            being open to change. “Some of the most successful companies have 
            had to let go of what made them successful and change, this requires 
            a lot of courage. Companies need to look forward rather than looking 
            back,” says Cunningham. <br>
            <br>
            The four decade old, Zubair group is preparing itself for the future 
            as exemplified by its advertising tagline – ‘Future Ready.’ The 
            operational functioning of the company has been handed over to 
            professionals. Says Cunningham, “We are investing in our key markets 
            but we recognise the fact that we need to create an investment 
            mindset. Traditionally we have tended to grow our business over the 
            long term, but now we are being pro active about return on 
            investments.”<br>
            <br>
            <img border="0" src="cover2.jpg" align="right" hspace="8" vspace="6" width="196" height="114">The company is also working on getting a better understanding of the 
            market. This is being done by stepping up its research efforts. The 
            exercise is expected to give the management a better understanding 
            of how to position the company. A chief strategy officer has been 
            appointed to oversee this exercise. “Our 40-year old legacy inspires 
            us but does not weigh us down,” says Cunningham. <br>
            <br>
            <b>HR focus </b> <br>
            Developing people capabilities is the other important ingredient of 
            building a robust institution. The Zubair group has rolled out the, 
            ‘Gold Programme for Graduates’ to train Omanis. Managing both for 
            the long term and short term is one of the biggest challenge faced 
            by CEOs. Cunningham avers, “Developing a long term strategy, but 
            delivering profits in the short term is a dilemma.” Luckily, the 
            tyranny of quarterly numbers is not as intense in Oman. Apart from 
            financial parameters, the group measures success on three other 
            parameters – the satisfaction of customers and employees and return 
            to shareholders.<br>
            <br>
