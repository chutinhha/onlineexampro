                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <br>
                </font><b><font face="Verdana" size="4">
                <img border="0" src="close.jpg" align="right" hspace="8" vspace="6" width="125" height="148">Bahrain: Ambitious but a 
                realistic budget </font></b><font face="Verdana" size="1"><br>
                <b>Public expenditure holds a special significance for Bahrain 
                as one third of the country’s GDP is accounted for by government 
                spending. The government is aware that steady spending is vital 
                to avert the threat of a financial crisis turning into an 
                economic challenge<br>
&nbsp;</b></font></p>
                <p align="justify" style="margin: 3px 10px"><b>
				<font face="Verdana" size="1" color="#CC3333">By Dr Jasim Husain 
				Ali</font></b><font face="Verdana" size="1"><b><br>
                </b><br>
                Steady spending is vital to avoid turning the financial crisis 
                into an economic one. At least this was the message of the 
                parliament during the process of budgetary approval for several 
                months. In fact, the parliament delayed its approval of the 
                two-year budget by some 70 days after the start of fiscal year 
                2009 to secure specific legislations pertaining to higher 
                spending. <br>
                <br>
                Amongst others, legislators insisted the inclusion of an 
                appropriation of Bahrain Dinar 100mn ($265mn) to help needy 
                families to cope with living expenses due to inflationary 
                pressures. Some 83,000 families will receive $132 every month in 
                2009 and 2010 to help deal with the consequences of inflationary 
                pressures and liquidity crunch. Members of parliament argued 
                convincingly that the economy was experiencing a unique 
                situation characterised by the absence of growth in prices on 
                the one hand and continuation of adverse effects of inflation on 
                the other. In other words, MPs pointed out to the phenomenon of 
                prices of many goods remaining high though not increasing 
                further. <br>
                <br>
                For fiscal year 2009, total expenditure amount to $5.5bn versus 
                an income of $3.7bn leaving behind a projected deficit of 
                $1.7bn. The statistics for 2010 suggests a spending of $5.8bn, 
                income of $3.9bn and a shortfall of $1.9bn. The figures compare 
                favourably with actual spending of $4.8bn in fiscal year 2007 
                (actual statistics for 2008 were not available at the time of 
                preparing this article).<br>
                <br>
                <img border="0" src="close1.jpg" align="left" hspace="8" vspace="6" width="200" height="543"><b>Oil dependency </b><br>
                The budget assumes an average price of $40 per barrel, in turn a 
                reasonable figure in current circumstances. Originally, the 
                authorities submitted the budget with an assumed oil price of 
                $60 per barrel. However, legislators pressed officials for a 
                more realistic oil rate in the light of prices in the first two 
                months of 2009. Eventually, the authorities used $40 per barrel 
                for 2009 and 2010, the same price as in fiscal year 2007 and 
                2008. However, such an assumed price was regarded conservative 
                in 2007. The petroleum sector accounts for 76 per cent of the 
                total income in both fiscal years, confirming the significance 
                of the hydrocarbons sector despite all the claims of economic 
                diversification. Bahrain generates its petroleum revenue from 
                sale of crude oil from both offshore and onshore fields plus 
                gas. Abu Saafa field, which is shared with Saudi Arabia, 
                accounts for around 80 per cent of the total oil revenue. 
                Bahrain’s own onshore field plus sale of gas accounts for 10 per 
                cent apiece. The balance state income (24 per cent) is generated 
                from the sale of utilities, customs on imports, return on 
                investments and grants extended by friendly countries. <br>
                <br>
                <b>Spending matters </b><br>
                Compared to 2008, projected expenditures are down by eight per 
                cent in 2009 and four per cent in 2010, reflected solely in 
                funds allocated for projects. However, the matter partly 
                reflects policy changes. Unlike earlier budgets, allocated 
                amounts for projects in 2009 and 2010 do not include figures for 
                electricity and water. The matter relates to setting up of an 
                authority for electricity and water, which in turn depends on 
                its own (generated) income to help financing its infrastructure 
                projects such as setting up substations (the other source of 
                spending on utility projects are private sector investors who 
                are interested in developing power plants). <br>
                <br>
                In other words, for the first time, electricity and water 
                commission (no longer a ministry) keeps its generated income for 
                spending on projects (but not manpower). It is believed that 
                spending on electricity and water projects would be more than 
                $265mn in each of 2009 and 2010. Not surprisingly, the utilities 
                authority has started the process of collecting delayed bills 
                even if it meant disrupting the service from households and 
                businesses. <br>
                <br>
                Separately, for the first time, the budget sets aside three per 
                cent of current spending for emergency purposes that arise 
                during the fiscal years. The legislation is meant to ensure 
                availability of funds to deal with emerging problems including 
                disasters without having to resort to unplanned borrowing. It 
                remains to be seen whether the authorities end up spending funds 
                set aside for development projects or repeat the practices of 
                the past few years. In 2007, officials spent merely 73 per cent 
                of appropriated funds for capital projects. The whole fiasco 
                relates to the fear of causing further inflationary pressures 
                through stronger demand. Another restriction concerned capacity 
                problem, namely shortage of supplies and qualified contractors.
                <br>
                <br>
                <b>Overcoming deficit </b><br>
                The statistics point out to serious budgetary deficit, 
                comprising about eight per cent of gross domestic product (GDP) 
                in each fiscal year. Yet, the planned monetary union in 2010 
                involving all Gulf Cooperation Council (GCC) countries except 
                for Oman limits budgetary deficit to three per cent of the GDP.
                <br>
                <br>
                Anyway, the deficit can be addressed in several ways. Chances 
                are that the actual deficit would be less than that projected in 
                both fiscal years. This assessment relates to the traditional 
                practice of real spending being lower than planned. Actual 
                spending on projects comprised 73 per cent, 76 per cent and 53 
                per cent of total allocated figures in 2007, 2006 and 2005, 
                respectively. The authorities attributed relatively low actual 
                spending in 2005 to delayed parliamentary approval of the budget 
                for fiscal years 2005 and 2006 (it was approved in May 2005). 
                Still, the parliament approved the budget for 2009-2010 in March 
                2009, thereby providing one such reason for potentially lower 
                than projected expenditures.<br>
                <br>
                Still, the authorities could partially rely on reserves 
                accumulated during the height of relatively high oil prices 
                (2003 to mid 2008) to partially finance the shortfall. Yet, 
                officials have the opportunity to increase debt level, which 
                currently stands at about 18 per cent of GDP. The planned 
                monetary union limits public debt to 60 per cent of GDP. The 
                authorities have not disclosed specific instruments for 
                financing the deficit, though this is expected to include a draw 
                on accumulated reserves. Public expenditures are uniquely 
                significant in Bahrain on the back of accounting for nearly one 
                third of the country’s GDP. Clearly, steady spending is vital to 
                avert the threat of turning the financial crisis into an 
                economic challenge with untold consequences. </font></p>
