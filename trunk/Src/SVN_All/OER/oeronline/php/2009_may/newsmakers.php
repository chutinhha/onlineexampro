			<b><font face="Verdana" size="4"><br>
			</font></b><b><font face="Verdana" size="4">Women power</font></b><font face="Verdana" size="1"><br>
            <b>HE Rajiha bint Adul Ameer bin Ali wins the Middle East Award for 
            Woman Leadership Personality</b><br>
            <br>
            <img border="0" src="newsmakers.jpg" align="left" hspace="8" vspace="6" width="250" height="258">Her 
            Excellency, Dr Rajiha bint Adul Ameer bin Ali, Minister of Tourism, 
            has received the Eighth Middle East Award for Excellence this year, 
            which is granted to women leaders with the best achievements. An 
            honouring ceremony at Arab Tower in Dubai was held to celebrate this 
            occasion. The ceremony was attended by Sheikha Hessa bint Khalifa 
            bin Ahmed Al Khalifa and a large number of leaders and officials 
            from various governmental sectors in the Middle East Region.<br>
            <br>
            Dr Rajiha in a statement of thanks and appreciation expressed her 
            happiness on receiving the award. She confirmed that this award is 
            an evidence of the prominent place given to Omani women on the 
            international and local level. Omani women have been able to attain 
            various achievements and contribute to the development of the 
            Sultanate due to the benevolent leadership of His Majesty Sultan 
            Qaboos bin Said. The minister added: “My nomination among many women 
            personalities from the Middle East region makes me proud of it, 
            especially the fact that these awards are for a personal ability to 
            bring about radical change and this will inspire other women to 
            assume leadership positions.” The Middle East Institute in Dubai 
            grants several annual awards to honour specialised entrepreneurs and 
            leaders who play a prominent role in fostering leadership amongst 
            women. </font></p>
            <hr color="#C0C0C0" size="1"> 
			<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
			<b><font face="Verdana" size="4">Green crusader </font></b><font face="Verdana" size="1">
            <br>
            <b>HH Sayyida Tanya Al Said has emerged as a mascot of environmental 
            sensibility<br>
            </b><br>
            <img border="0" src="newsmakers1.jpg" align="right" hspace="8" vspace="5" width="250" height="249">HH Sayyida Tanya Al Said, founder and president of the Environment 
            Society of Oman (ESO), is one of the six winners of ‘Environment 
            Personalities of the Year Awards’ given by the GCC this year. Since 
            her return from the UK, where she studied Marine and Freshwater 
            Biology, HH Tanya has been an untiring and consistent fighter for 
            preservation of the environment. HH Tanya’s enterprise, the ESO, set 
            up in 2004, is the only environment NGO in Oman.<br>
            <br>
            Sayyida Tanya feels that it is imperative to educate the public 
            about the detrimental effects of plastic bags, littering and wastage 
            of water in Oman. She also believes that it is just changing one’s 
            behaviour and habits for saving the environment that is needed. HH 
            Tanya calls for making full use of our natural resources and 
            intelligence to conserve water and energy. She advocates the use of 
            solar energy for heating the overhead water tanks on all new 
            buildings, recycling plastic bags and bottles and conserving water 
            by planting native plants (which require less water) by the roads. 
            The water saved in this way could be used for other things. She 
            would very much like to see more information to be included in the 
            school curriculum on environment – this could include local case 
            studies. Her NGO the ESO has been undertaking projects with schools 
            such as recycling plastic bottles along with Masafi and the National 
            Mineral Water Company. Combined with environmental awareness drive, 
            HH Tanya hopes to garner help from the various governmental bodies 
            to spread her ideas for a more eco friendly and secure environment.</font></p>
