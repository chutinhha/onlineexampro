		<b><font face="Verdana" size="4">Fostering efficiency</font></b><font face="Verdana" size="1"><br>
		<b>From inception to production, Tony Kinsman has led Sohar Aluminium from 
		the front<br>
		</b>
		<br>
		<img border="0" src="NEWSMAKERS2.jpg" align="left" vspace="5" width="298" height="167">The $2.4-billion Sohar Aluminum Company, the first greenfield smelter in 
		the Gulf region for more than 25 years, has begun production at its 
		350,000 tonne-per-year location in Sohar. “This is a very proud moment 
		for everyone at Sohar Aluminum, from the shareholders and management 
		team to all operators, maintainers and staff,” Sohar Aluminium Chief 
		Executive Tony Kinsman said on the occasion. The start-up was witnessed 
		by Sohar Aluminum Chairman Ahmed Al Wahaibi and a host of company 
		officials and other special guests.<br>
		<br>
		Kinsman has been with Sohar Aluminum since the inception and has been a 
		guiding force in building the team to achieve the goals of the company. 
		The management, led by Kinsman, admires the perseverance of the 
		operations teams, which prepared for the start-up under difficult 
		conditions. <br>
		<br>
		Sohar Aluminum is a joint venture – 20 per cent owned by Alcan, the 
		Canadian aluminium producing unit of Australia-based Rio Tinto, while, 
		Oman Oil Company and the Abu Dhabi Electricity and Water Authority each 
		hold a 40 per cent stake.<br>
		Sohar Aluminium is one of only three aluminium producers in the Gulf 
		Arab region. As well as achieving its construction and production 
		targets, the Sohar Aluminium project has also managed to stay on budget 
		throughout the entire design, procurement and construction phase, noted 
		general manager Geoffrey Brookes. <br>
