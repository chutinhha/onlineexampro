		</font><font face="Verdana" size="4"><b>Taking Off<br>
		</b></font><font face="Verdana" size="1"><b>Oman’s first private 
		credit bureau is off to a promising start</b><br>
		<br>
		<img border="0" src="NEWSMAKERS.jpg" align="right" vspace="5" width="298" height="189">Small investors in the Sultanate of Oman have a lot to cheer about now, 
		with the establishment of Oman’s first private credit bureau by the 
		National Bureau of Commercial Information (NBCI). <br>
		<br>
		It was promoted by Hind Bahwan, who is the chairperson of the credit 
		bureau, and by Khalid Ahmed Al Hosni, who is the vice-chairman and 
		managing director. Says Sheikh Hosni, “we offer information on 
		individuals ranging from his credit history to his financial details. 
		This can be of great help to companies and banks looking at lending or 
		offering facilities to consumers. The CMA has passed certain regulations 
		that will govern the operation of NBCI.” <br>
		<br>
		As an integral part of a strong financial infrastructure, credit 
		reporting bureaus have been identified as one of the main providers of 
		basic information for forming credit strategies such as the ones 
		prescribed by the new Basel II Capital Accord. NBCI has teamed up with 
		Experian, one of the three largest global information services 
		companies, to acquire immediate technical know-how, robust systems and 
		instant credibility. NBCI will provide valuable decision-making tools to 
		any company or financial institution extending consumer credit in the 
		Sultanate.<br>
		<br>
		Although credit bureau reports do not include any sort of credit 
		decision opinions, they provide the basic data to institutions giving 
		credit for evaluation of portfolios and individual account risk ratios.<br>
		The credit bureau will be capable of collating, sorting and producing 
		credit reports on individuals, small and medium-sized businesses on a 
		real-time, web-based platform.<br>
