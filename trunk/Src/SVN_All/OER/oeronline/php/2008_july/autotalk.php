        </font><b><font face="Verdana" size="4">QUIET BEHEMOTH</font></b><font face="Verdana" size="1"><br>
        <br>
        <b>Lincoln navigator impresses with its performance and style writes 
        Malcolm Xavier CRASTA</b><br>
        <br>
<img border="0" src="AUTOTALK2.jpg" align="right" vspace="5" hspace="5" width="250" height="153">Lincoln has long since been a relatively quite company in Oman. Now it 
        seems, they have woken up in style. The release of the new Navigator 
        seems to be just the start of what’s to come. This month we test the new 
        overhauled Navigator to see how it compares to its competition.<br>
        <br>
        <b>Exterior</b><br>
        To start with they have really ‘blinged-out’ the vehicle. Up front, not 
        one but two large chrome grilles stare you in the face. This chrome look 
        is carried over to the rest of the car as well with large chrome strips 
        down the side of the car and chrome accents on the mirrors and under the 
        windows followed by some more chrome lining around the tail lamps and 
        finally ending with a massive chrome Lincoln cross. While this may seem 
        a bit excessive, it works well in giving the Navigator’s subtle styling 
        some character. We feel that the chrome will also have a huge effect on 
        the exterior colour of the vehicle, so choose carefully. Overall the new 
        Navigator is a good improvement over the older model and grabs a bit 
        more attention than asked for.<br>
        <br>
        <b>Interior</b><br>
        Stepping into the cabin is like a time warp back to the seventies. 
        Everything from the flat dash to the square dials and even the gear 
        lever design feels nostalgic with a seriously modern touch-up. This 
        feels refreshingly new in this day and age. While the dash trim may look 
        a bit on the bargain side, it actually is of very good quality and is 
        more than a match for the Europeans. This same quality follows through 
        to the rest of the cabin. <br>
        <br>
        Thanks to its huge size there is more than adequate space to seat seven 
        people in the utmost comfort, regardless of height. Getting a 
        comfortable driving position was not really a problem, thanks to the 
        generous seat and pedal adjustments. Although not really necessary 
        because of the pedal adjustments, it was quite surprising that the 
        steering wheel was not adjustable for reach. This is complemented by 
        seats designed by the Ford-owned Volvo which, while not as supportive as 
        they are in the Swedish company’s cars, are very comfortable. With all 
        seats up, there is more than enough room in the boot to do your 
        shopping, but for weekend trips you can fold down the second and third 
        row seats, the third is power folding, for a truly cavernous boot.<br>
        <br>
        Lincoln has not gone soft on features either. The car has available 
        heated and cooled bucket leather seats, a fourteen speaker THX certified 
        audio system, dual headrest mounted DVD players, DVD navigation system, 
        second row cool box and a lot more. Combine this with a host of safety 
        features, all-round airbags, ABS, roll stability control and more, and 
        you end up with a premium SUV that is more than a match for any of its 
        competitors, most of which cost significantly more.<br>
        <br>
        <b>Drive</b><br>
        Keeping in mind that this is a 2,700kg behemoth, it was respectably 
        agile. Through corners there was the expected body-roll but grip was 
        maintained without loosing composure. Regardless, this vehicle was not 
        built to take corners, it was built to carry seven people in utmost 
        comfort and this is something it does perfectly. It is quite possibly 
        one of the most refined cars available on the market today. Cabin noise 
        is kept to such low levels that it will give Lexus a run for its money, 
        and combined with its supple ride it makes for a very soothing place to 
        be for that long weekend trip to Dubai.<br>
        <br>
        Although the Navigator is a great long-distance hauler, powered by a 
        300hp(net) 5.4l V8, it struggles slightly when pushed hard. It is 
        perfectly adequate as long as long as you don’t expect too much out of 
        it. The test-drive vehicle we drove was not equipped with front parking 
        sensors which would have been extremely useful. Thankfully, this can be 
        easily solved since all existing models can be fitted with the sensors 
        at the dealer itself and we were informed that all future models will be 
        fitted with them directly from the factory.<br>
        <br>
        <b>Verdict</b><br>
        While it may not be the fastest or most nimble of SUV’s; it is refined, 
        comfortable, well equipped and is a breeze to drive despite its size. It 
        will suit anyone who is looking for a comfortable premium class seven-seater 
        and prioritises comfort.
