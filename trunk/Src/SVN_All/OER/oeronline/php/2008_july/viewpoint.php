<br>
</font><b><font face="Verdana" size="4">What Do You need: Phone? Camera? Music 
Player?</font></b><font face="Verdana" size="1"><br>
<b>Technology is bound to throw up more and more surprises in the days to come. 
But purveyors of technological products ought to impress upon the end users its 
utility, rather than the technology<br>
</b><br>
Are you from Oman Mobile? Asked the gentleman standing next to me in the shuttle 
coach at New Delhi international airport when I was on my way to Muscat in 
mid-June. Oman Mobile? Me? I was a bit flummoxed Realising that I was 
bewildered, he pointed at the Oman Mobile logo printed on the armband of my 
T-shirt. I was wearing the Oman Economic Review CEO Golf 2005 white bodied, 
blue-check collared sportswear. Oman Mobile was – and continued to be year after 
year since then - the sponsor for the most talked about annual jamboree for CEOs 
of the Sultanate, which is organised by our publishing group.<br>
<br>
By 
the time we boarded the flight, he knew this history behind my Oman Mobile 
linkage. Mike Gauba, an internationally renowned, high technology management 
consultant, was on his maiden visit to Oman on an assignment. The Sydney-based 
knowledge warrior was expected to assist an unnamed Omani corporate entity in 
getting into the mobile application business. In his anxiety to fast-learn about 
the Sultanate and its existing ICT (information, communication and technology) 
culture, he sacrificed his business class seat to be near me in the economy 
class! <br>
<br>
The professor, who taught business at the Korea University a few summers ago, 
had turned entrepreneur of late and had also some time ago advised a Saudi 
entrepreneur for three months. He was categorical that people by and large are 
‘tech-shy’. “Give them a multi-utility gadget and they will be wary of using. 
The subconscious mind refuses to accept such a complicated item, though nothing 
is complicated,” he said amidst the constant engine roar and mid-air turbulence. </font>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
To buttress his argument, Gauba cited a real-life incident. At one of his client 
sites, the tech-savvy CEO had installed a multipurpose gadget: 
fax-cum-scanner-cum-xerox-cum-phone and advised his colleagues to ‘get used to 
it’. Expectedly, not all were happy. But, they could do very little. However, 
sensing the staff resistance or reluctance, a simple, plain vanilla photocopying 
machine was installed next to the multi-purpose apparatus. The office voted for 
the plain one, much to the CEO’s chagrin. <br>
<br>
Gauba was not off the mark. I carry an iPod. I also carry an 80GB hard disk 
wherever I go. Though I am fully aware of iPod’s capability to store data and 
have tested it out, I am still not comfortable using iPod as a data storage 
device. For me, iPod is an audio-video player. Just that. <br>
<br>
Technology is bound to throw up more and more surprises in the days to come. But 
purveyors of technological products ought to impress upon the end users its 
utility, rather than the technology itself. “Blackberry is a hot selling item 
not because it is a tech marvel, but it is positioned as one gadget that will 
keep you connected through email always,” added my co-traveller. Blackberry, a 
phone or mobile PC? Gauba wonders. <br>
<br>
In a similar vein, Gauba attributed iPhone’s success to brand strength and 
argues that iPhone is a sheer waste of time and money! Who wants thousand 
utilities bundled into one single gadget? he asked rhetorically. Steve Jobs and 
his million geek fans may not agree. But, that’s beside the point. <br>
<br>
Communication is critical. The sense of urgency and purpose becomes sharper when 
one deals with communicating technical issues. When driving a car, you and I are 
more interested in experiencing driving; not in understanding how the system 
functions. How the fuel injection pump works is of least consideration – unless 
you are a car aficionado. Speed matters and thrill counts – not the 
nerve-rattling technical jargon. <br>
<br>
Of late, markets are flooded with mobile phones that play music as well as the 
Sony Walkman used to do; and mobile phones that handle FM broadcasts also. Then 
there are phones with cameras to satisfy the photo-hungry public. “The human 
psyche seeks simple solutions. The phone is meant for a precise purpose. Music 
players were again created for a specific objective. When someone mixes it up, 
it challenges the subconscious. Unless you are a geek, it is tough handling a 
multi-utility product,” elaborated the professor from Sydney. <br>
<br>
Back in office, I saw a media report saying that Oman was making concerted 
efforts to spread IT in the country with the help of IT majors. Dr Salim Sultan 
al Ruzaiqi, CEO of Information Technology Authority (ITA), had gone on record 
emphasising that Oman’s digital strategy was going ahead as planned with the 
Sultanate being ranked 28th internationally in supporting the growth of 
knowledge and technological environment. <br>
<br>
As I crosschecked with a colleague about an email sent to a senior ministry 
official a few days ago seeking some information, he advised me to send a fax. 
What’s wrong with an email request? I asked. Why take a risk? Retorted my 
colleague. Are we not talking about the total roll-out of e-governance: a 
paperless government-public interaction through the Internet? Where does the 
Sultanate stand, honestly? <br>
<br>
Dr Firas Al-Abduwani of Hussam Technology Company, came to my rescue. “Way back 
in 2002 when I returned from the Netherlands after my Ph.D, it used to take four 
days for a one gigabyte file to download in Muscat as against 120 seconds in The 
Hague. Today, we are much closer to the global standards. Things have changed 
drastically. Yes, there is always room for improvement.” <br>
I clicked on my wi-fi. Bingo! In less than 30 seconds I landed in the web world. 
I am truly blessed…and connected too. <br>
