<p align="justify" style="margin: 3px 10px"><b><font face="Verdana" size="4">
<br>
Focusing on Growth</font></b><font face="Verdana" size="1"><br>
<b>The Shanfari Group is gearing up for another spell of growth after a 
successful restructuring. Nathalia Jones&nbsp; reports</b><br>
<br>
</font><font face="Verdana" size="1">From a humble beginning almost four decades ago, when it launched a fledgling 
construction business in Salalah in 1960, the Shanfari Group has made giant 
strides in business and industry across the Sultanate. Today, the group has 
aggressively and astutely forayed into a wide cross-section of industrial 
sectors, commanding a strong presence in construction, manufacturing, 
hospitality, oil and gas, travel and tourism, logistics and virtually every 
other aspect of business.</font><font face="Verdana" size="1"><br>
<br>
Now the conglomerate is gearing itself up for yet another growth spell and the 
recent restructuring within the company has set it on an ambitious expansion 
path where strategic joint ventures and higher value addition will be the key to 
success.<br>
<br>
Recently, at the World Economic Forum Summit, the Shanfari Group was elected as 
a global growth company for maintaining a consistent growth rate of 15 per cent. 
Now that it is in a stage of transition, there is considerable optimism that the 
growth rate will surpass this level. The group’s chief operating officer, 
Sandeep Mishra, describes the group’s current strategies as “challenging but 
achievable”, adding that it will certainly find the group becoming more focused 
and its goals translating into action.<br>
<br>
“We have restructured the group in terms of finance and manpower, so now our 
business plans are more focused. That is to say, we are translating our 
objectives and targets into an action plan and we are conducting periodical 
reviews and assessments of our achievements and targets. We are more focused 
now….we are clear and flexible in the goals we’ve set,” said Mishra.<br>
<br>
<b>Expansion plans</b><br>
<img border="0" src="corporate_profile.gif" width="290" height="216" align="right" hspace="6" vspace="4">The restructuring has brought renewed focus on expansion and to that end the 
company is in the process of forging strategic alliances with external partners. 
“We are also identifying our strengths and where to form strategic alliances to 
get better results in terms of technology and output.”<br>
<br>
“So, we are contemplating a number of joint ventures in areas where we think we 
can collaborate with foreign companies and technology providers,” he said. For 
instance, in the oil and gas sector, Shanfari has collaborated with the US-based 
Enfab and Oman Metal Industry, who are global pioneers in manufacturing steam 
generators and instruments for processes in the oil and gas sector. In yet 
another instance of strategic ties, the group has entered into partnership with 
Sambu Construction, the Korean-based company, for a major project in Salalah, 
which involves the dualisation of the Salalah-Thumrait road. “We are also trying 
to be an EPC company in oil and gas, construction and electromechanical 
engineering and we are in talks with viable partners,” said Mishra.<br>
<br>
Other key areas of interest in the coming years would be mining, manufacturing 
and construction, water treatment and services for the oil and gas sector, said 
Mishra. “We are looking at consolidating our core activities, i.e. construction, 
oil and gas, and product manufacturing, where there is competitive advantage of 
the country in terms of having availability of mineral/natural resources. For 
instance, spurred by the recent surge in oil prices, hydrocarbon producers are 
using record oil revenues to finance the use of exploration techniques that were 
previously considered too expensive. Enhanced oil recovery (EOR) techniques are 
becoming commercially viable now. We are already in the area of manufacturing of 
EOR equipment and will like to further consolidate and expand in this area,” he 
added.<br>
<br>
In terms of business expansion, Mishra explained, that the group was looking at 
horizontal and vertical expansion, and with regard to market expansion, 
geographical diversification through the introduction of new products was key to 
the action plan for growth. Mishra saw immense opportunity for business 
diversification in areas such as water treatment. This, he anticipated, would be 
of considerable economic interest in the years to come, presenting opportunities 
that far surpassed prospects in the oil and gas sector. <br>
<br>
“True, the oil and gas sector yields a profitable business, but the value and 
usage of water presents a far more significant opportunity in that it is 
versatile,” said Mishra. The company has already embarked on water treatment 
projects such as one in the Mukhaizna oil field for which it is collaboratively 
working with Aquatech, the US-based company.<br>
<br>
Similarly, the Shanfari Group is also investing considerable interest and 
resources in yet another sector that has witnessed a surge in activity – mining.<br>
<br>
<b>Key mineral supplier </b><br>
The Sultanate’s rich and abundant mineral reserves have drawn serious 
participation and investor interest globally, and its position as a key mineral 
supplier is significant, specially in the context of the global construction 
boom. These two factors have heralded a new era of opportunity and business 
prospects for companies interested in mining activities, a lucrative venture 
that hasn’t gone unnoticed by the Shanfari Group, said Mishra. The company is 
already in talks with major companies from India and the West for developing 
strategic collaborations in mining activities. <br>
<br>
“We are focusing on mineral resources for the metal industry. Oman is full of 
mineral resources, which have not been fully exploited so far. There’s no mining 
based industry out here. Chrome, for instance, is available here in plenty, but 
it’s not being developed. So, we are looking at companies who are interested in 
this developing this mineral, an essential for the steel industry. We are not 
only talking about extracting the mineral and exporting it, but we would like to 
do is to work for beneficiation (an electrolysis process) of the chrome ore 
prior to exporting it, so there is value addition.”<br>
<br>
“Similarly, there is an abundance of limestone, a key raw material for cement 
and steel industry. All limestone required for the steel industry in India, for 
instance, is being sourced from Ras Al Khaimah, a part of the UAE. Oman’s 
resources have not been exploited, so we are in talks with companies to develop 
them.”<br>
<br>
“Oman is also full of silica sand, which is crucial to the glass industry. But, 
we don’t have any major manufacturing unit here, apart from Majan Glass. So, 
there is a lot of potential to create a product from the raw materials available 
here. It is also an area of interest,” pointed out Mishra.<br>
<br>
However, automotives would continue to drive the company forward in the 
attainment of its goals, said Mishra, pointing out that the Shanfari Group would 
like to retain its position in the niche segment of high-end brands.<br>
<br>
‘We cater very well to all segments of the market – at the top end we have 
Ferrari, Lamborghini and Maserati, and at the mid sector, we have Jeep, 
Chrysler, Dodge and Saab. You have all the brands available in Oman and the 
market is very competitive. Hence, churning out profit is not so easy. But we 
have niche marketing for our top-end brands. Similarly, in the four-wheel drive 
segment, the Jeep is a very popular brand. Moreover, to have an edge in the 
market, we concentrate on 3S facilities – sales, service and spares through the 
best practices and keep on enhancing the skills of our technicians and 
professionals,” said Mishra.<br>
<br>
The scale and extent of the group’s ambitious plans of expansion and 
diversification require a huge intake of human resource and Mishra was only all 
too aware of the current talent crunch and retention issues. So, the Shanfari 
Group is “looking at experienced and skilled manpower. We look at how to recruit 
the best and how to retain them, and retention is becoming a challenge, unless 
one offers the right combination of authority and execution power,” Mishra 
pointed out.<br>
<br>
So, what then are the challenges that lie ahead and how does Oman provide a 
conducive environment to effectively tackle these hurdles? “It’s a small market, 
with a population of just 2.6 million. So, everything depends on the population. 
But in terms of infrastructure, the market is there and it is growing. The 
country is still at the take-off stage. The oil and gas sector is growing, 
specially with the renewed emphasis on EOR. The challenge for trading is that 
though there is a wide variety in terms of brands that are available, there are 
a limited number of consumers,” Mishra concluded. <br>
