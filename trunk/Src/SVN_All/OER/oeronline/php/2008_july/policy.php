<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<br>
</font><b><font face="Verdana" size="4">Creating trust and security 
<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="POLICY.jpg" align="left" vspace="5" hspace="5" width="265" height="250">The E-law will help customers migrate to an online platform giving a fillip 
to Internet-based commerce in the Sultanate, writes Mayank Singh<b><br>
</b>
Oman moved one step closer to creating a digital society by introducing an 
electronic transaction law or E-law on May 17, 2008. The measure is a part of 
the Sultanate’s national IT strategy. Says Dr Salim Sultan al Ruzaiqi, CEO, 
Information Technology Authority (ITA), “One of the most important aspects of 
building an e-governance environment is to create trust. The E-law is a platform 
that creates this.”<br>
Following the Royal decree promulgating an E-law, all Internet-based 
transactions acquire a legal validity. For example, an electronic signature will 
have as much validity as a physical signature. Armstrong Jeyakumar, MBA 
Programme Manager and Lecturer, Majan College explains, “Cyber laws or E-laws 
regulate commerce which may, fully or partially, be transacted online. These 
laws are based on the premise that e-commerce is structurally different from 
conventional trade and so need a different set of regulations.”<br>
&nbsp;</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
<b>The spectrum</b><br>
E-law covers a wide range of areas pertaining to electronic commerce like buying 
and selling on the Net, credit card transactions, domain names, contractual 
liability for defective hardware and software, terms of Internet contract, data 
protection etc. The adoption of online commerce needs two things – trust and 
security. These two attributes can be guaranteed only by a legal environment. 
For example, retail transactions like buying and selling on the Internet 
involves a financial transaction creating the need for legal protection. An 
E-law is essential to identify the parties involved and bringing in an 
authentication of the deal. “Once these two criterion is fulfilled a transaction 
becomes secure,” says Jeyakumar. Moreover in case of a breach of contract both 
the parties are aware of the legal redressal at their disposal.<br>
&nbsp;<br>
The law is expected to give a big fillip to online commerce in the Sultanate. 
Till now the use of online banking in the Sultanate has been in the low double 
digits but analysts expect this to grow at a rapid clip from here. The ITA is 
creating a central portal for all electronic transactions taking place in the 
Sultanate. Any person can log onto the portal using a log in name and a 
password. This portal will facilitate online commerce and protect the interest 
of the parties involved. <br>
<br>
<img border="0" src="POLICY2.jpg" align="right" vspace="5" hspace="5" width="237" height="250">Though an online process saves time and cost most companies in the Sultanate 
encounter the last mile problem as customers are hesitant to try out the 
electronic option due to security concerns. With a comprehensive set of cyber 
laws in place consumers will be more confident about pushing the execute button. 
Says M K Janaki Raman, General Manager, Bahwan IT, “All companies and 
individuals will gain from the law.” <br>
<br>
As Oman emerges as an important investment destination for international 
investors, there has been a growing demand for such a law. “Whenever we speak to 
companies abroad, there are always queries about an E-law. With this law in 
place organisations like OCIPED can promote Oman as an attractive investment 
destination,” says Ruzaiqi.<br>
<br>
<b>Distance traversed</b><br>
The law has been in the works for more than a year now as law firms from the 
Sultanate and abroad worked out the fine print. Luckily, the authorities did not 
have to reinvent the wheel completely as a number of precedents for such a law 
exist. The United Nations E-law being the most well known one. The Council of 
European Convention 2007 has a separate section on cyber laws. Though such 
references exist there was a need to localise them to suit the needs of the 
Sultanate. A gap analysis was carried between the prevailing laws and what was 
needed and a final draft was drawn. The ITA is following up the E-law by sending 
it to different ministries asking them to update their regulations in accordance 
with the Royal decree.<br>
<br>
<b>The law was published in the gazette on June 2, 2008, brining it into force
</b><br>
To make the law effective there is a need to generate awareness about its 
provisions. Says G Gopalakrishnan, CEO, Jawad Sultan Technologies, “A number of 
measures can be taken to combat e-fraud if people get to know about cyber laws 
and their usage.” The ITA has lined up a number of initiatives for the purpose. 
One, the details of the legislation are being put up on the ITA website. Second, 
a full day workshop on E-law will be conducted in Salalah in August. Third, the 
E-law will get its fair share of voice during ITA’s second roadshow this year.
<br>
<br>
Another critical piece of the jigsaw is to train the stakeholders or government 
officials. The ITA is rolling out a massive effort to train 93,000 government 
employees from the third quarter of 2008. All government departments are being 
brought onto the same IT platform creating a seamless process. Says Raman, “ITA 
is taking a lot of steps in the right direction and this will have a good 
impact.”<br>
<br>
<b>Concerted efforts </b><br>
<img border="0" src="POLICY3.jpg" align="left" vspace="5" hspace="5" width="226" height="246">As the Internet is not bound by national boundaries, crimes on the Net are 
international in nature, raising a question mark on country-specific laws. 
“There is a need for countries to accept the idea of dual criminality wherein a 
person who commits a cyber crime is considered to be a criminal not only in that 
jusridiction where the crime has been committed but also in the country where he 
resides,” says Jeyakumar. This is a complicated issue as it calls for combined 
sleuthing by various countries.<br>
<br>
Aware of the need for more co-operation, the ITA is working closely with FIRST, 
a global non profit organisation in the field of Internet security. FIRST brings 
together a variety of computer security incident response teams from government, 
commercial, and educational organisations to combat Internet based crimes. Says 
Ruzaiqi, “FIRST has a project called CERT which works in the area of Internet 
security and we are establishing its centre in the Sultanate.” A number of 
hacking incidents recently have been traced back to Russia and the authorities 
are working on curbing them. <br>
<br>
Scandinavian countries have had the most advanced ICT laws followed by North 
America and East Asia. “Cyber regulations fit in with their focus on the quality 
of life,” says Jeyakumar. In the region Bahrain, Qatar and the UAE have cyber 
laws and with the E-law in place, Oman is set to join the comity of nations 
which is in the vanguard of promoting a digital society. <br>
