<b><font face="Verdana" size="4">Reduce, Reuse, Recycle</font></b><font face="Verdana" size="1"><br>
<b>Recycling is important. But recycling is only one part of the 
environment-friendly business equation. Some tips on operating a green business</b><br>
<br>
Operating a green business is not only good for the environment, but good for 
your business’ bottom line because conserving resources and cutting down on 
waste saves money. Recycling is important. But recycling is only one part of the 
environment-friendly business equation. One can also take a large step towards 
being more environmentally-friendly by reducing the amounts of waste in our 
offices and business operations.<br>
<b><br>
Here are ten easy-to-implement ideas for running a green business </b><br>
1. Turn off equipment when it’s not being used. This can reduce the energy used 
by 25 per cent; turning off the computers at the end of the day can save an 
additional 50 per cent.<br>
<br>
2. Encourage communications by email, and read email messages onscreen to 
determine whether it’s necessary to print them. If it’s not, don’t!<br>
<br>
3. Reduce fax-related paper waste by using a fax-modem and by using a fax cover 
sheet only when necessary. Fax-modems allow documents to be sent directly from a 
computer, without requiring a printed hard copy.<br>
<br>
4. Produce double-sided documents whenever possible. Reuse printer ribbons, so 
you will throw away fewer plastic cartridges. Keep an old ribbon for test prints 
and a new one for final copies.<br>
<br>
5. Find a supply of paper with maximum available recycled content. Also, choose 
suppliers who take back packaging for reuse.<br>
<br>
6. Instigate an ongoing search for ‘greener’ products and services in the local 
community. The further your supplies or service providers have to travel, the 
more energy will be used to get them to you.<br>
<br>
7. You can compensate for the greenhouse gases emissions released in the process 
of generating the power your PC uses by buying carbon offsets. A growing number 
of companies invite you to help reduce overall carbon emissions by planting more 
trees or purchasing “carbon credits” on the global market. Most such companies 
focus on balancing out the carbon from driving or air travel, but some offer a 
chance to calculate (and offset) the greenhouse impact of your energy use.<br>
<br>
8. Use up your supply of disposable pens, mechanical pencils, lighters - 
anything meant to be used once and tossed. Replace them with refillables and 
reusables and buy bulk refills.<br>
<br>
9. Replace incandescent light bulbs with compact fluorescents. According to 
Fifty Simple Things You Can Do to Save the Earth by The Earth Works Group, 
compact fluorescents cost more, but they last more than 100 times longer and use 
a quarter of the electricity.<br>
10. Choose an environmentally-sound product and pick a manufacturer that offers 
a good recycling programme. Many major vendors offer such products, and there’s 
no performance downside. In some cases, you may even get free merchandise or 
discounts when you support recycling programmes. 
</font></p>
