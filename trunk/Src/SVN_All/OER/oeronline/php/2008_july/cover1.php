<p align="justify">

<b><font face="Verdana" size="4"><br>
</font></b><b><font face="Verdana" size="4">A change in attitude</font></b><font face="Verdana" size="1"><br>
<br>
<br>
<b>Hussain Jawad, Chairman, W J Towell, shares his thoughts on the younger 
generation</b><br>
<b>How do you view the younger generation of business leaders?</b><br>
<img border="0" src="COVER-STORY7.jpg" align="right" vspace="5" hspace="5" width="194" height="250">As most of these youngsters are born with a golden spoon in their mouth, they 
lack the seriousness of the older generation. Their parents have to take on a 
large part of this blame, as in most cases they are too keen to give their 
children a good life. What they do not realise is that they are harming them, 
instead. Today, people are motivated primarily by a financial or moral reason. 
If people have less money, it motivates them to work hard. Once you are 
financially satisfied, one gives back to society and this is the moral drive. 
Since today’s youngsters are financially well off from day one, they have no 
inclination to work hard. If he gets a Bentley and a Porsche from the day he 
enters a business, he will have no drive to do better. This is a problem being 
faced by most business families. <br>
<br>
<b>What are the efforts being taken within W J Towell to deal with this problem?</b><br>
We are putting in place more criteria before someone can join the family 
business. For example, we want the next generation to go and work somewhere else 
before joining the family business. This helps them to learn about their 
weaknesses and improve themselves. If they joined the family business straight 
away, they would never be aware of their shortcomings. I advise all business 
families to take this grooming process seriously or otherwise they risk losing 
everything that they have built. In a family business, a family member’s 
contribution is not measured from day one; it is more about being trained to 
carry on the job in the next five to six years. I do not see the new generation 
taking on the same responsibilities as the older generation. There are other 
issues that at times make things difficult. For example, in a family you cannot 
criticise others directly, but have to convey things indirectly. We have a 
communication meeting every Wednesday where such things are communicated. <br>
<br>
<b>Is there a gender divide that you see in the new generation?</b><br>
There is a lot of difference between girls and boys. Girls are more responsible 
and serious. They are also more mature at the same age compared to boys, but 
since they get married outside the family, they are not in a position to 
contribute to the business.<br>
&nbsp;<br>
<b>Is the new generation more educated or technically more qualified than your 
generation?</b><br>
For the last 20 years, there is no education gap between our children and us. We 
are as technologically savvy as them. So nobody can hold that against us. 
Returning to work, it is essential for people to deliver results and give back 
to society.</font></p>
