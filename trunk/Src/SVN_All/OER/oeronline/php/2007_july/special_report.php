<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">No holds Barred</font></b><font face="Verdana" size="1"><br>
<b>By virtue of its rapid strides in development, the Middle East continues to 
attract multi-national companies. P Aneel Kumar, during his recent ‘Build Middle 
East’ media tour, finds out how GE is redrawing its growth strategy to maximise 
business in the region<br>
</b><br>
<img border="0" src="sp.jpg" width="300" height="419" align="left" hspace="6" vspace="4">At the end of May, a group of journalists from around the world gathered for 
lunch on the 25th floor of Burj Al Arab. The conversation centred around the 
rich and the famous who had bought ‘several countries’ in The World. A 
Saudi-based journalist was keen to know who purchased the USA, while some others 
wanted to know whether David Beckham bought England or Britney Spears lapped up 
France. No one was quite sure who bought which country, though they know for 
sure that the countries in The World were up for grabs.<br>
<br>
For the uninitiated, the countries we are talking about are small islands being 
developed under a gigantic infrastructure project called The World – a 
collection of private islands that form a map of the continents of the world, 
off the coast of Dubai. It is being developed by the world’s largest privately 
held real estate developer, Nakheel. This is just one of the several such 
projects that redefine the modern day Middle East. Mega projects like Burj 
Dubai, The Palm and Dubai Economic City in Dubai, King Abdullah Economic City 
near Jeddah, Blue City near Muscat, The Pearl in Qatar, The Silky City in Kuwait 
and New Town in Bahrain are all part of ‘building’ the new Middle East.<br>
<br>
<b>Land of opportunities<br>
</b>The region, which has over 40 per cent of world’s oil reserves and more than 
20 per cent of world’s gas reserves, is currently among the most attractive and 
competitive regions in the world. For instance, more than US$1 trillion 
investment is expected by 2030 in the energy sector alone. About US$700 billion 
will be put in the region’s construction projects by 2010. No wonder then that 
global conglomerates are eying for a piece of pie here.<br>
<br>
Take the case of General Electric Company (GE), which has its presence in the 
region since 1930. With products and services ranging from aircraft engines, 
power generation, water processing and security technology to medical imaging, 
business and consumer financing, media content and advanced materials, GE serves 
customers in more than 100 countries and employs more than 300,000 people 
worldwide. With so much on offer in the Middle East, the company is planning to 
build its business in the region with a special focus on high-growth sectors. 
Led by a strengthening of the regional infrastructure, industrial and healthcare 
businesses, GE expects an average annual growth rate of 20 per cent through to 
2010. “The roadmap to growth will be underscored by effective compliances among 
the people, products, processes and technology,” said the company officials.<br>
<br>
Due to its active participation in the high-growth business sectors, the 
company’s revenues grew from US$1.3 billion in 2001 to US$5.5 billion in 2006. 
It registered over 32 per cent growth in 2005. The company’s main growth drivers 
in the Middle East are technical and commercial innovation in six core 
businesses – GE Infrastructure, GE Industrial, GE Healthcare, GE Commercial 
Finance, GE Money and NBC Universal. The region has traditionally been a strong 
market for GE Infrastructure and GE Healthcare, both of which have contributed 
34 per cent and 29 per cent growth in revenues, respectively. It is one of the 
region’s leading employers with more than 2,300 professionals.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="sp1.jpg" width="450" height="268"><p align="center" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b>Future vision<br>
</b>“GE’s growth strategies for the MEA region are aligned with the development 
vision of the regional economies, focused on building for the future,” said 
Nabil Habayeb, GE’s president and chief executive officer for the Middle East 
and Africa region. “The region offers significant growth potential in energy, 
healthcare, transportation, aviation and oil &amp; gas – all of which are core 
strengths of GE,” he added.<br>
<br>
GE Infrastructure is closely working with companies like Dubai Holdings, Emaar, 
Nakheel and Dubai Economic City on projects like Burj Dubai, The Palm, The World 
and King Abdullah Economic City. On the other hand, GE Industrial is trying to 
make most of the US$700-billion opportunity in construction sector. It plans to 
further extend its business by increasing its human resources by over 49 per 
cent. It also plans to set up a joint venture on circuit breakers, sensing 
service centre and a centre of excellence on oil &amp; gas.<br>
<br>
On the healthcare front, GE Healthcare is opening a manufacturing facility in 
Saudi Arabia, which will focus on manufacturing innovative diagnostic imaging 
systems to meet the growing demand for modern healthcare equipment regionally. 
In another initiative, GE Healthcare has signed a Memorandum of Understanding (MoU) 
with the Saudi Arabian Ministry of Health to train more than 1,000 Saudi 
healthcare professionals annually.<br>
<br>
<b>New initiative<br>
<img border="0" src="sp3.jpg" width="300" height="201" align="right" hspace="6" vspace="4"></b>In a strategic diversification initiative, GE Money, the global 
consumer-lending unit of the company, signed a joint venture with the UAE-based 
Al Futtaim Group to form a new company that will roll out several consumer 
finance products including auto and personal loans, leases, sales finance, bank 
cards and insurance. <br>
<br>
In addition, GE’s television and entertainment business, NBC Universal, has made 
its mark in the region. Developed by Tatweer, ‘Universal City Dubailand’, a 22 
million sq ft development within Dubailand was launched last month. The 
centrepiece of the project, Universal Studios Dubailand, will be amongst the 
world’s largest theme parks. The city will also include hotels, retail outlets, 
commercial offices and residential compounds. <br>
<br>
John Rice, Vice Chairman of GE, says GE is focused on building sustainable 
foundation in the region through effective public-private partnerships in 
sectors as wide ranging as oil &amp; gas, infrastructure, healthcare, aviation, 
security and finance. Highlighting on these partnerships, Rice said GE had 
formed strategic joint ventures in the region – two of them in Saudi Arabia with 
Ali A Tamimi Company – the Middle East Engineering Ltd (MEELSA) and Middle East 
Power Company Limited (MEPCO) in Saudi Arabia. “The MEELSA workshop has been 
operational since 1976, and we are working with authorities to further our 
investment and make MEELSA a state-of-the-art repair and training centre to 
serve the needs of Saudi Arabia and the region,” explained Rice.<br>
<br>
Besides, GE’s Water and Process Technologies has joined hands with Ali A. Tamimi 
Co. to set up a specialty chemicals blending unit based in Dammam. GE has also 
agreed to a US$20-million deal to supply mobile water treatment equipment to Al 
Tamimi Group. “The equipment will enable Al Tamimi to quickly provide temporary 
or emergency water filtration equipment to municipal and industrial clients in 
Saudi Arabia and across the region,” Rice added.<br>
<br>
Further expanding its local capabilities in the region’s power generation 
industry, GE Energy is establishing new power generation projects offices in the 
Middle East to coordinate project management activities for the region, where 
the company has project commitments of more than US$4 billion.<br>
<br>
Considering that GE is undergoing so much activity in the region, Steve Bolze, 
President of Power Generation for GE Energy, explains, “Middle-East is one of 
the most important growth areas for GE in the world today.” </font>
