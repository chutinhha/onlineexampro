      <p align="justify"><b><font face="Verdana" size="4">King Abdullah Economic 
      City<br>
      </font></b><font face="Verdana" size="1"><br>
      <b>Location</b>: Built at a pristine location off the Red Sea, north of 
      Jeddah<br>
      <b>Focus</b>: Port and logistics, light industry and services<br>
      <b>Size</b>: 168 million sq m<br>
      <b>Investment Size</b>: US$27 billion<br>
      <b>Employment</b>: 1 million<br>
      <b>Population</b>: 2 million<br>
      <br>
      “Our vision is to position Saudi Arabia among the top 10 most competitive 
      nations by 2010 through the creation of a pro-business environment, a 
      knowledge-based society and by developing new, world-class Economic 
      Cities,” His Excellency Amir Abdullah Al-Dabbagh, governor and chairman of 
      the board of Saudi Arabian General Investment Authority (SAGIA), told a 
      visiting international media delegation at King Abdullah Economic City. In 
      line with this vision, the biggest economy in the region is developing six 
      economic cities, which are best described as ‘pockets of competitiveness.’ 
      These cities are expected to put Saudi Arabia among top 10 most 
      competitive economies in the world by 2010. And the biggest city, also the 
      largest private sector initiative in the Kingdom, is the King Abdullah 
      Economic City (KAEC).<br>
      <br>
      SAGIA, the apex body responsible for inward investments into Saudi Arabia, 
      is the prime facilitator for KAEC, which is being developed by Emaar the 
      Economic City, a consortium headed by Emaar and a number of high profile 
      investors from Saudi Arabia. With a share capital of US$2.26 billion, the 
      company offered about 30 per cent to the public in September 2006.<br>
      <br>
      “KAEC is a mega multi-stage development, which will closely integrate 
      itself into the Kingdom’s ongoing drive to expand the economy, create 
      employment opportunities for its youthful population and function as a 
      catalyst to attract foreign investment. Based on initial forecasts, the 
      project and its several components will create up to 500,000 employment 
      opportunities in the City,” said Nidal Jamjoom, CEO of Emaar EC. The City 
      will be a mixed-use development located north of the commercial hub of 
      Jeddah to ensure easy access from the holy cities of Makkah and Madina. A 
      massive 55 million sq. m of Greenfield land with a 35-km shoreline close 
      to the industrial city of Rabegh, has been earmarked for the project. It 
      will have six distinct components – a seaport, industrial district, 
      financial island, education zone, resorts and the residential area.<br>
      <br>
      Groundwork on KAEC is going full stream ahead. Work is underway on the 
      project’s first boulevard – a 15-km long stretch that features over 3,000 
      palm trees. Excavation work for the first city canal to run through the 
      Red Sea Village has also started. Construction of phase 1 of road network 
      will start this summer, and a rail link connecting Makkah, Jeddah, and 
      Madina will be undertaken. The construction investment in 2007 alone is 
      expected to touch US$580 million.<br>
      <br>
      During the first phase, the City is expected to create 2,000 anchor jobs 
      during the first three years of operation. It will start handing over 
      industrial plots, residential and commercial units in Q4 of 2008, and 
      begin operation of the port in the first half of 2009.<br>
      <br>
      By 2026, the City will house about two million people in about 168 million 
      sq. m, with 150,000 apartments and 22,000 villas. The port will have the 
      capacity of 10 million containers over an area of 14 sq. km. Some of the 
      other highlights of the City include 11,000 hotel rooms, more than 2,500 
      factories, 3.8 million sq. m of office and multi-purpose space, 8.7 
      million sq. m for commercial use, and 4.5 million sq. m of greenery and 
      open spaces (natural beaches extend along 14 km).</font><p align="center">
