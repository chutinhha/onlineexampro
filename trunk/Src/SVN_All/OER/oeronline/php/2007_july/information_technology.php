<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Oman can be the Gulf’s IT hub<br>
</font></b><font face="Verdana" size="1"><b>Tariq Hilal Al-Barwani, Oman’s popular IT expert and Microsoft’s Most
Valuable Professional, shares details of his rendezvous with Bill Gates in 
an exclusive write-up for OER<br>
</b>
<br>
<img border="0" src="it1.jpg" width="136" height="173" align="left" hspace="6" vspace="4">I was surprised and thrilled when the world’s largest software company, 
Microsoft, appreciated my efforts to promote IT in the local and international 
community. Microsoft honoured me with Most Valuable Professional (MVP) Award, 
the first ever for an Omani national. <br>
<br>
I was invited by Microsoft to attend the MVP Global Summit 2007 in Seattle, 
Washington. The summit was conducted at the Washington State Convention Trade 
Center and Microsoft’s main campus in Redmond. Of course, Bill Gates kicked off 
the event with a keynote address in which he shared his insights, thoughts and 
vision for technology, in addition to the future growth and development of 
Microsoft. Later, Gates interacted with invitees in a Q&amp;A session. It was great 
meeting the richest man in the world, who is worth US$56 billion (as of March 
2007).<br>
<br>
At the MVP Global Summit, the recipients, drawn from all over the world, were 
appreciated for their talent and contribution. We received exclusive access to 
Microsoft’s technical content, participated in direct feedback sessions, and met 
with the company’s executives, product teams and peers from around the world. I 
was very pleased to see the flag of Oman fluttering among the other 
award-winning countries. I had the chance to meet, discuss and share ideas with 
a number of people, on various topics related to technology, development and 
future insights.<br>
<br>
Utilising the unique opportunity, I promoted Oman as a hot destination for 
investments in the IT industry. A large number of people showed keen interest in 
knowing more about the technology park – Knowledge Oasis Muscat (KOM) – to 
explore opportunities in information and communication technologies in Oman.<br>
<br>
More than 400 technical sessions were organised during the summit in which MVPs 
met each other, created relationships with Microsoft experts and provided 
real-world insights and feedback to technological solutions. It provided the 
MVPs an opportunity to exchange information with Microsoft executives and 
product managers, share best practices, network, as well as have fun.<br>
<br>
<b>Inspired by Bill Gates<br>
</b>During the keynote address and presentation, I 
found that Gates expressed himself very openly, directly and candidly. No one 
had to guess what 
his true feelings on the subjects were – it was like an open book. All of us 
were inspired by Gates, as he enthused us to think, plan and take positive 
action in our life and profession. He 
meets challenges in his life and career with an open mind and great enthusiasm. 
His adventurous, courageous and independent attitude and personality cast a 
great spell on those aspiring to reach his level of achievement. I think Gates 
has a natural flair and instinct about understanding what people want, and he 
responds to them accordingly. He knows the pulse of the market and accordingly 
develops products. Today, we are driven majorly by Microsoft products; be it in 
PCs, mobiles or entertainment systems. Gates had an idea, a plan and worked hard 
towards it – that’s the secret of his success on
his own terms.<br>
<br>
After appreciating the achievements and efforts of MVPs, Bill Gates began by 
discussing the magic of software. He said Microsoft, throughout its entire 
history, had focused on one thing, and that was building great software, both as 
a platform and as a set of direct tools that empower people to do new things.<br>
<br>
<img border="0" src="it.jpg" align="right" hspace="6" vspace="4">Gates also talked about the transition that we’ve gone through recently, i.e., 
from 32-bit to 64-bit computing. Using a typical desktop machine, he highlighted 
the growth of computing, pointing out that today such machines were powered by 
two processors. He said five years from now such machines would host more like 
16 or 32 processors, and even more at the level of the server. He also looked at 
the hardware aspects (the graphics yesterday, today and what tomorrow holds in 
terms of crystal clear looks and feel, and how Microsoft was working on 
utilising the technology).<br>
<br>
Bill then looked at corporate training, what it is today and how it will change 
completely. All education and lectures were going online; in some cases for 
free. He also looked at the evolution of the television media, pointing out that 
while earlier we always used to think of a TV with a limited number of channels. 
But with the evolution of the information superhighway, videos are now being 
delivered to one, based on the shows one might be interested in and one cares 
about, all customised, based on one’s likings and desires.<br>
<br>
Gates also illustrated how the shape of the data centre may change in the 
future. He said Microsoft and others, who are into large-scale internet 
services, were building data centres that today have hundreds of thousands of 
computers but in the future centre would literally have millions of computers. 
They were making sure that even if one computer failed, the system would 
automatically recover. He also detailed the techniques being applied in data 
centres, even going down to very small ones.<br>
<br>
The summit made it clear to me that the Sultanate should emphasise more 
aggressively on the importance and benefits of technology, as this medium can 
surely be an additional means of income for the country. There has to be an 
increased stress on the importance of education, especially in schools where 
students are introduced and equipped with the necessary IT skills. There’s a lot 
of potential in Oman. I’m confident that Oman can do more to become the hub of 
the Gulf in technology, of course, with the right resources and mindset on 
board.
