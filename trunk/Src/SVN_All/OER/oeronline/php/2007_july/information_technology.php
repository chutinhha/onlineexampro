      <b>Tariq’s persona</b><br>
Tariq has been carrying a number of achievements under his belt since the age of 
five – from creating softwares such as virus cleaners, system check-up and 
optimisation for the benefit of the public, assisting the then sole telecom 
company (Omantel) to developing an Internet application to serve its customers, 
