<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Angel in America</font><font face="Verdana" size="1"><br>
Unheralded Angel Cabrera has won the US Open with a rock-solid final round, but 
only after Tiger Woods made some puzzling miscues and let a second straight 
major slip away<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="golf.jpg" width="200" height="318" align="left" hspace="6" vspace="4">The only thing Angel Cabrera couldn’t conquer at this US Open was Oakmont. That 
was much tougher than Tiger Woods. Cabrera hit all the right shots at the right 
time, none bigger than a booming tee shot down the middle of the 18th fairway 
that stopped a late slide, allowed him to post a 1-under 69 and forced Woods and 
Jim Furyk to catch him. For the second straight major, Woods couldn’t buy a 
birdie. For the second straight US Open, Furyk
couldn’t make a par.<b><br>
<br>
</b>Cabrera became the first Argentine in 40 years to win a major, powering his way 
to a pair of birdies on the back nine that turned into a one-shot victory over 
two of the best players in the world. “It is very difficult to describe this 
moment,” Cabrera said. He is not the player anyone expected to take down Woods. 
But neither was Zach Johnson at the Masters.<br>
<br>
OK, so Cabrera is not a normal guy from Cedar Rapids, Iowa. He is a former 
caddie at Cordoba Golf Club in Argentina, who dropped out of elementary school 
to provide for his family and figured golf was his only hope to make a decent 
living. He fits the image of Pittsburgh tough, big and burly, walking down 
fairways while puffing on a cigarette. “There are some players that have 
psychologists,” Cabrera said. “I smoke.”<br>
<br>
Cabrera wore a smile to hide his nerves as he sat in the clubhouse and watched 
Furyk fall out of a share of the lead on the 17th hole by trying to drive the 
green 306 yards away. The shot went into deep rough left of the green, his flop 
shot came up short and his 8-foot par putt swirled around the lip. That left 
only Woods, who needed a birdie over his final three holes to force a playoff. 
He did well to two-putt for par on the 244-yard 16th. His bunker shot on the 
17th went over the green and made him again grind out a par, and a tee shot he 
thought was perfect on the 18th instead straddled the first cut and thick rough 
right of the fairway. Woods hit wedge that went 30 feet beyond the pin, and his 
birdie putt was too strong. Once again, just like at the Masters, Woods was the 
last player to walk off the 18th green with the trophy belonging to someone 
else.<br>
<br>
“Finishing second is never fun,” Woods said after closing with a 72. “You play 
so hard, and it’s just disappointing.”<br>
<br>
Cabrera not only beat the No. 1 and No. 3 players in the world, his birdie on 
the final hole has caused No. 2 Phil Mickelson to miss the cut in a major for 
the first time in eight years. The big-hitting Argentine earned this one.<br>
<br>
“I beat everybody here, not only Tiger Woods,” Cabrera said. “But I wasn’t able 
to beat the golf course. The golf course beat me.”<br>
<br>
Oakmont turned into a survival test with its thick rough and scary greens, 
although Cabrera handled it better than anyone. Of the eight sub-par rounds all 
week, he had two of them, and finished at 5-over 285.<br>
<br>
