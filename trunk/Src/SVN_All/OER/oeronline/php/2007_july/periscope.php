<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">A bullish case for Russia<br>
</font><font face="Verdana" size="1">Russia has US$380 billion in hard currency 
reserves and its natural gas crown jewel Gazprom commands a market cap second 
only to Microsoft and General Electric, writes Matein Khalid<br>
</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1">
<img border="0" src="news10.jpg" width="156" height="242" align="left" hspace="6" vspace="4">Russia has been one of the world’s 
great emerging markets success stories in this millennium. When Boris Yeltsin 
anointed Vladimir Putin as his successor as the Russian President on Christmas 
Eve 1999, the Kremlin was still devastated by the trauma of the rouble 
devaluation, the collapse of oil prices and the de facto bankruptcy of the 
banking system. However, Russia’s financial renaissance under Putin since 2002 
has coincided with a three-fold increase in oil prices, a boom in precious metal 
and commodities from diamonds to nickel to natural gas, and, above all, a 
secular bull market in Moscow. Russian sovereign debt, in default only nine 
years ago, has since been upgraded no less than eight times to investment grade 
by Standard and Poor’s, and Moodys.<br>
<br>
Russia has US$380 billion in hard currency reserves and its natural gas crown 
jewel Gazprom commands a market cap second only to Microsoft and General 
Electric. Russia trades at a huge valuation discount at 12 times earnings to its 
fabled BRIC emerging markets’ of China (40X on Shanghai A shares) and India 
(22X). Moreover, Russia’s second largest bank VTB just floated its shares in 
London and Moscow in a US$8-billion IPO, putting selling pressure on the 
secondary market. The RTS Index has dropped more than 200 points since it 
reached a historic peak above 2000 in early April.<br>
<br>
The Russian stock market is not down due to a macroeconomic malaise. After all, 
with 8 per cent GDP growth, US$380 billion in central bank reserves and a surge 
in real wages, domestic investment and money supply, Russian econometrics are 
among the finest in the emerging markets. The market has gone down only because 
US$12 billion in IPO deals have forced fund managers and retail investors to 
sell blue chips to buy the newly issued shares. Yet VTB, the old Soviet Trade 
Bank, whose loans and deposits are growing at 30 per cent a year is Russia’s 
only second listed bank after Sberbank. VTB was the largest offering in the 
history of the Russian stock market, with 150,000 bids for the Moscow rouble 
tranche of the IPO. While hardly on the colossal Chinese ICBC scale, the VTB IPO 
heralds the birth of an equity culture in Russia with profound implications for 
the stock market’s evolution.<br>
The surreal Litvinenko espionage-murder scandal and the geopolitical Cold War 
rhetoric have spawned a new chill in Russian equities among international 
investors. While China’s ICBC spawned global hysteria during its IPO last 
October, VTB has risen only six per cent since it was floated on the London 
Stock Exchange. So even cheap valuation, eight per cent growth and a US$70 price 
for North Sea Brent crude oil cannot compensate for the political risk embedded 
in Russian valuations. After all, the Kremlin’s nationalists moves against Shell 
and BP as well as the use of gas as a political weapon against Ukraine and 
Georgia have contributed to the exit of many fund managers <br>
from Russia.<br>
<br>
However, politics aside, Russia’s financial metrics are irresponsible. The mere 
60 basis point spread in Russian government debt above US Treasury notes is a 
testament to the world’s most spectacular sovereign credit upgrade story of the 
past decade. Like Japan after World War II, Russian consumers have seen their 
wealth and income increase four-fold since 2002, a major reason that Vladimir 
Putin remains popular despite his authoritarian administration in the Kremlin. 
Unlike the failed Marxist-Leninists of the Soviet Union or Yeltsin’s capitalist 
chaos, Putin has delivered economic growth and financial stability for Russia, 
despite his determination to exclude foreign investors from controlling interest 
in strategic industries, such as the oil &amp; gas. After all, even the US did not 
allow CNOOC to buy Unocal for its Southeast crude oil reserves.<br>
<br>
The liquidity squeeze in Russia has created an attractive entry level for 
long-term investors. Shares of Russian oil major Lukoil have gone nowhere while 
the shares of Petrobras and Sinopec have surged 25 per cent since Christmas. 
There is ample historical precedent to Russian stock markets rallying after a 
surge in IPOs as liquidity returns to the secondary markets.<br>
<br>
Unlike India, Vietnam, China, with their 100-million brokerage accounts or the 
GCC, where retail money was the catalyst behind a parabolic bull market, Russia 
is the only major emerging market that has not experienced a retail driven, bull 
market. Of course, the litmus test for Russian retail investors will be the 
performance of VTB, whose shares will also be a favourite of offshore money 
managers, as they will be added to the Morgan Stanley Russian Index.<br>
<br>
The rise in the Treasury bond yield to 5.20 per cent and the spike in volatility 
on the Wall Street means it is imprudent for investors to be long on greed and 
short on fear on absurdly valued stock markets like China, Vietnam and India. 
Beijing has made no secret of its determination to avoid a 1990s Nikkei 
Dow-style stock market bubble that could mean financial Armageddon before the 
Middle Kingdom’s coming out party at the 2008 summer Olympics. The volatility of 
the Chinese stock market will only rise as a Democratic Congress in Washington 
unleashes a protection sentiment and accuses Beijing of currency manipulation 
with its deliberately undervalued exchange rate. Russia thus could well prove a 
hedge against protectionism and an oil shock in 2007. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
