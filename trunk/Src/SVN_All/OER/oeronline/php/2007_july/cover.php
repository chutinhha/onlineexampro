</font><font face="Verdana" size="4">
‘Made in Oman’ 
surging ahead<br>
</font><font face="Verdana" size="1">
<b>Oman is set to cross the RO1-billion mark in non-oil exports in 2007 riding on 
the growing popularity of ‘Made in Oman’ products. In an exclusive report, 
Akshay Bhatnagar finds out about the growth trajectory of such products, the 
roadmap ahead and the related challenges</b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<img border="0" src="cover1.jpg" width="350" height="233"></font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
In ancient days, Oman, then known as Majan, was at the forefront of 
international maritime trade as Omani dates, traditional handicraft and 
fragrances were very popular in Africa, Middle East, Europe and Asia. With the 
discovery of hydrocarbon reserves in the 20th century, like rest of the other 
Gulf states, Oman’s economy shifted focus to the oil and gas sector. However, 
with the increased stress on diversification of the economy, Omani products 
across different categories are coming to the fore and gaining wider 
acceptability in the international market.<br>
<br>
<b>Rapid growth<br>
</b>In fact, the strong growth recorded by exports of non-oil products of Omani 
origin has been one of the most impressive aspects of the country’s 
macroeconomic performance in 2006. Growing by 46.3 per cent over 2005, the value 
of non-oil exports (excluding re-exports) stood at RO812.3 million in 2006. In 
2002, these were worth just RO261.6 million.<br>
<br>
In its annual report for 2006, the Central Bank of Oman (CBO) says: “the growth 
of 46.3 per cent achieved in 2006 cannot be seen as a blip, since in the last 
three years, annual non-oil export growth had consistently been above 30 per 
cent in every year, and going by the prospects of a large number of 
export-oriented industrial projects starting actual operations in the near 
future, the momentum on the non-oil export front could be expected to be 
sustained over time.” The bank noted that the 2006 growth was driven to a large 
extent by the 327.9 per cent increase in exports of mineral products (cable 
pipes, iron and steel products, etc.). It was followed by the chemicals and 
allied industries sector, which registered a growth of 55.5 per cent in 2006, 
over and above the 218.6 per cent growth seen in 2005. The strong performance of 
this sector was primarily due to the export of fertilisers by Oman India 
Fertiliser Company (OMIFCO), which was initiated in 2005. On the other product 
categories, the Bank said, “in the non-oil export basket, most of the other 
items, such as live animals and animal products, vegetable products, foodstuffs, 
etc., exhibited negative growth, reflecting the growing demand
within the economy for such items in the face of rising inflation. Export of 
textiles and articles thereof continued to exhibit the negative trend that 
started in 2004, reflecting primarily the impact of termination of the 
multi-fibre agreement (MFA) related quotas under the WTO.” However, it is the 
changing portfolio of the non-oil exports’ basket that indicates the shift in 
the economy’s thrust on manufacturing industries. In fact, it was 2006 that for 
the first time, non-oil exports of Omani origin overtook re-exports (valued at 
RO766.8 million) from the country.</font></p>
