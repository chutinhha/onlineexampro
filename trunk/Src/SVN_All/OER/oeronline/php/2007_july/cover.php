<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<br>
<b>Destination of non-oil exports</b><br>
Even though Oman’s non-oil exports market is quite broadbased, the concentration 
is very high. UAE and India together accounted for almost 55 per cent of the 
country’s non-oil exports in 2006. According to the CBO report, “Non-oil exports 
to the UAE registered a sharp growth of about 65 per cent. As a result, the 
share of UAE in total non-oil exports increased further from 34.9 per cent in 
2005 to 39.5 per cent in 2006. India emerged as the second most important market 
for Oman in 2005 with a share of 12.7 per cent, which further increased to 15.3 
per cent in 2006 on account of higher export of fertiliser by OMIFCO.” (See 
table: Destination of non-oil exports.)<br>
<br>
Despite the growth in trade between the US and Oman, the share of the US in 
Oman’s total non-oil exports has been going down. From 7.6 per cent in 2004, it 
declined to 3.4 per cent in 2005, further to just 1.9 per cent in 2006. But, 
with the US-Oman FTA expected to come into force shortly, the trend is expected 
to change, as more Omani exporters will target the US market. Similarly, exports 
to other advanced countries, whose currencies had exhibited significant 
cumulative appreciation against the US dollar since 2002 (such as the Euro-area 
and the UK), did not show much increase. These were also not the leading 
destinations for Oman’s non-oil exports. In fact, the potential competitive 
advantages that should have arisen following the sustained depreciation of the 
US dollar were not there, as is made clear by the country-wise information on 
Omani exports.<br>
<br>
<b>Way forward</b><br>
Given the past performance and following reports that the Gonu cyclone is not 
expected to make a significant dent, non-oil exports from Oman are set to exceed 
RO1 billion in 2007. But what about 2008 and beyond? As per projections made by 
the Omani Centre for Investment Promotion &amp; Export Development (OCIPED), Oman is 
targeting non-oil exports to touch RO2,750 million by the year 2010. That means 
an almost five-fold growth in the period 2005-2010, with a compounded annual 
growth rate (CAGR) of 38 per cent. Certainly, not a mean task ahead!<br>
<br>
On its part, OCIPED has chalked out a strategic framework to achieve this 
target. According to an OCIPED official, “We have prioritised the products for a 
focused approach. The products in the thrust area have been segmented into four 
categories.” The first, high-value products whose exports are valued at RO50 
million and above. Urea comes in this category. It does not require any major 
support from OCIPED. The second is of medium-value-products, whose exports are 
worth RO5-50 million. They are likely to grow rapidly and may need support 
initiatives that are generic like infrastructure, trade pacts, etc. Electric 
cables, steel tubes, marble, vegetable oil, steel bars and rods, BOPP films and 
products, aluminium profiles, lead acid, copper wire, detergents, biscuits and 
other cocoa products, potato chips and condiments, among others fall in this 
category.<br>
<br>
The third category is of low-value products, whose exports are between RO3 and 5 
million. They have the potential to graduate to the medium band and may need 
product-specific support from OCIPED and other entities. Products in this 
category include steel billets, ceramic tiles, metal furniture, plastic 
tableware and kitchenware, rubber and plastic mattresses, and air conditioners, 
among others. The last is a special category comprising products whose exports 
are currently between RO1 and 3 million but which have a good growth 
opportunity. Frozen fish, dates, glass containers, canned tuna, surgical gloves, 
tomato paste, switchgears, transformers, electric heaters and mushroom are part 
of this category.<br>
<br>
<b>Challenges in export promotion</b><br>
Even though large-scale industries with major foreign investments are coming up 
in Oman – especially in Sohar – to further boost non-oil exports, the medium and 
below category products as per OCIPED’s segmentation will have to make a 
significant contribution if the targets are to be achieved. The task is not so 
easy as Omani non-oil exporters face many obstacles in penetrating international 
markets. In an era of mass manufacturing targeted at catering global demand, 
Omani companies lack the economies of scale to compete with giant corporations. 
However, some companies, like Oman Cables, Amouage and Al Waqia Shoes, have 
built comparatively larger production facilities despite the small size of the 
domestic market and are successfully marketing their products internationally. 
Nevertheless, support from export promotion bodies needs to be upscaled.Hussain 
Bin Salman Al Lawati, Vice Chairman &amp; Managing Director of Oman Cables says, 
“All the Omani entrepreneurs are not as enterprising as Oman Cables. Most of 
them need support and guidance. The government has done a great service on this 
front in the last three decades. But I feel that efforts of the government and 
the export promotion organizations are not properly channelized to optimally 
utilize all the opportunities available in the international market. To make 
matters worse for the small and medium class manufacturing industry, the export 
promotion bodies have not considered to invest in viable and potential 
development areas with appropriate financial inputs which will help Oman’s 
export very greatly.”<br>
<br>
He adds, “In order to save pennies, we lose out on big opportunities. We cannot 
expect to bag orders by merely participating in the international exhibitions, 
which actually brings awareness of the existence of business than real orders. 
We have to invest our time and money and continue to exhibit our products on a 
sustainable basis for few years before we start getting the desired results. The 
companies that cannot afford to do this should be supported with financial 
assistance to participate in export promotions and these export promotion 
bodies, in turn need adequate and committed funds for such exercises. The 
authorities also needs to introduce tangible benefits for the best performing 
exporters.”<br>
<br>
Like Al-Lawati, Hussain Ahmed Ghuloom, the CEO of Al-Waqia Shoes Co LLC – 67 per 
cent of whose sales comes through exports – also advocates the need for 
aggressive marketing. “Marketing is the key component for any Omani product to 
succeed in the international market. In general, Omani products have proved to 
be of good quality but the lack of marketing and little knowledge about the 
foreign markets has been a major obstacle for most Omani products to click.”<br>
<br>
Thus, the key to achieving the targeted RO2.7 billion in non-oil exports by the 
year 2010 lies in gathering adequate market intelligence and pursuing aggressive 
marketing. More opportunities are expected to come Oman’s way through the trade 
agreements with the US, EU and India. Is it going to be a smooth affair or prove 
to be a challenging task, that’s a billion riyal question!</font></p>
