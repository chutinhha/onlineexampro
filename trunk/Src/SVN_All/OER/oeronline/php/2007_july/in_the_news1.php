<b><a name="Woman_on_top">Woman on top</a></b><br>
    </font><font face="Verdana" size="1">
	<img border="0" src="news8.jpg" width="200" height="220" align="right" hspace="6" vspace="4">On September 18, 2006, Anousheh Ansari hit the headlines the world over, 
becoming the first female private space explorer. Originally from Iran, she has 
now settled down in the US. Akshay Bhatnagar spoke to her on the sidelines of 
the Women in Business 2007 conference. Excerpts:<br>
<br>
<b>When you moved from Iran to the US, you were 16 years old, what was it like?<br>
</b>The first struggle there was to learn a new language and get assimilated in a 
new culture. It helped me develop my survival instincts. <br>
<br>
<b>How did you become an entrepreneur?<br>
</b>My husband and I were working in a telecom company called MCI Telecommunications 
and one day it moved its headquarters to a different city. We quit the company 
and started our own consultancy in the telecom space. That was the beginning of 
my entrepreneurial journey, one that I always wanted to embark upon. 
Subsequently, we got into software development for telecom companies. Our 
company, Telecom Technologies Inc., apart from other products, made a soft 
switch to enable voice over IP communications. Later on, we sold the company for 
US$750 million.<br>
<br>
<b>What you did with that money?</b><br>
While we had a majority stake in the company, our employees and other investors 
also had a substantial ownership. We invested our share of the proceeds in 
promoting other companies working towards developing cutting edge technologies 
apart from social sector. We also formed a new technology company called Prodea 
Systems in 2006.<br>
<br>
<b>What kind of products is Prodea Systems working on?<br>
</b>Prodea Systems will dramatically alter the nature of the in-home technology 
experience. It will help customers unleash the power of the internet and realise 
the full potential of their digital homes. The product is under incubation and 
it will be commercially available towards the end of the year.<br>
<br>
<b>Do you plan to extend your business to Middle East?</b><br>
I hope so. The market is a perfect fit for our products.<br>
<br>
<b>What is your role in the company?</b><br>
I’m the chairman and COO of the company. I look after the operations of Prodea 
Systems. <br>
<br>
<b>What are your goals for the coming years?</b><br>
I hope to further develop my interest in the space business. I would like to 
work towards bringing down the cost of orbital launches. In the education 
sector, I’m working on a project in partnership with other organisations to 
promote science and technology.<br>
<br>
<b>Are you also looking at writing a book?<br>
</b>I started a blog before my space trip. It became very popular. Millions visited 
my blog and thousands left their comments. I’m now putting together a book about 
my life and experiences that will be ready for release sometime next year.<br>
<br>
<b>As a successful businesswoman based in the US with roots in the Middle East, how 
do you look at the state of women in this region?<br>
</b>I’m very pleased with the developments taking place, specially in the Gulf 
countries. The women here are making the best use of resources and opportunities 
available to them. Despite certain obstacles, they are emerging as a significant 
contributor to the development of the society and the economy as a whole. I see 
a great future for women in the region. Here, I would like to tell those people, 
who believe that women who wear traditional Islamic clothes, such as hijab, will 
not be successful, that they are wrong. What we wear cannot be an impediment to 
what we intend to accomplish. <br>
<br>
<b>You have consciously stayed away from expanding your family to accomplish your 
goals in life. What is your advice to women who are caught between the quagmire 
of family and career?<br>
</b>I believe that if I have children, my life will not be mine anymore. It will 
belong to them. I think it is a matter of individual choice. There are many 
successful women who have families and have maintained a fine balance between 
career and family.</font></td>
