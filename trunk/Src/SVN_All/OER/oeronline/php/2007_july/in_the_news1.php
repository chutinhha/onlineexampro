<font face="Verdana" size="4">Excelling despite challenges<br>
</font>
<font face="Verdana" size="1"><b>A woman’s work is never done, so an old adage goes. However, for many women in 
the Arab world, the work is yet to begin. Akshay Bhatnagar reports on the Women 
in Business Conference that along with the latest IFC study on women 
entrepreneurs in the MENA region showcases the brighter side of the picture</b></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="news7.jpg" width="450" height="253"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b><br>
</b>The world-over, more and more women are seeking economic opportunity and 
self-determination through enterprise creation. Earlier cited purely on an 
anecdotal basis, the growth of women’s entrepreneurship is today being 
increasingly covered and commented upon in the business media. Today, as most 
surveys and statistical sources reveal, 
it appears that between one-quarter and one-third 
of businesses in the formal sector worldwide are owned and operated by women; 
the share of informal enterprises owned by women is even greater.<br>
<br>
Along with increased media attention has come a marked increase in the efforts 
to support the growth of women business owners and their enterprises by national 
public policy-makers, multilateral organisations, and non-governmental 
entrepreneurial support organisations. <br>
<br>
In Oman, in recognition of the vital role played by women in the development of 
the national economy, the Oman Chamber of Commerce and Industry (OCCI) has 
launched a businesswomen committee. “The committee aims at improving the role of 
Omani businesswomen, to support their leading role in society, to communicate 
their aspirations and requirements, to provide all necessary assistance for 
carrying out their activities and to make Omani businesswomen capable to 
undertake active role in the economic development,” says Lujaina Mohsin Haider 
Al Za’abi, chairperson of the Committee.<br>
<br>
<b>Women in business</b><br>
It was to promote women in the business arenas and corporate world that the 
first ‘Women in Business Conference’ was held in Muscat on June 2-3. Organised 
under the patronage of HH Sayyida Aliya Bint Thuwaini Al Said, the conference 
brought together hundreds of successful businesswomen and female entrepreneurs, 
who
shared their experience and exchanged ideas.<br>
<br>
Michelle Mone, founder and co-owner of the UK-based MJM International Ltd., 
advised participants to “grab every piece of PR because it can make a massive 
difference in your business. Never ever take eyes off your business. When you 
take your eyes off, everything goes wrong. Never ever become complacent in life, 
keep tight control on your costs and ban the ‘can’t-do’ attitude from your 
lives.”<br>
<br>
Anousheh Ansari, the first female private space explorer and co-founder and 
Chairman of Prodea Systems, pointed out that, “in many countries, women are not 
encouraged to pursue careers in science and technology. However, they should 
decide what they want to do and follow their dream.”
(<a href="#Woman_on_top">See interview below</a>).<br>
<br>
Aneth Arosemena, division head, marketing and sales, Oman Mobile, advised women 
to come forward and break the barriers, seek opportunities, create a friendly 
work environment and turn women attributes into effective tools at work. <br>
<br>
<b>Entrepreneurial spirit</b><br>
Female entrepreneurship in the MENA region has been increasing and becoming more 
visible over the last decade. Women-owned businesses are contributing to 
economic growth and wealth creation. They are creating employment opportunities 
for other women (and men as well). Moreover, economically active women represent 
a potentially profitable market niche for the financial sector.<br>
<br>
In June, the International Finance Corporation and Center of Arab Women for 
Training and Research jointly published a comprehensive report titled ‘Women 
Entrepreneurs in the Middle East and North Africa: Characteristics, 
Contributions and Challenges’. The report was based on a study conducted in UAE, 
Bahrain, Jordan, Lebanon and Tunisia. According to Edward A. Nassim, VP (Europe, 
Africa and Middle East), IFC, “the purpose of this analysis is to help countries 
in the MENA region better understand the challenges faced by women and to 
promote an environment in which female as well as male entrepreneurs can 
thrive.”<br>
<br>
The report states that women business owners surveyed in MENA were well ahead of 
their counterparts in Western Europe and North America with respect to the size 
of their firms and many report substantial levels of revenue. When compared on a 
USD equivalent basis, between 6 per cent (Jordan) and 33 per cent (UAE) of 
surveyed enterprises were generating more than US$100,000 per annum – comparing 
favourably to the 13 per cent share found among women-owned firms in the United 
States.<br>
<br>
Most survey participants owned established businesses and many had extensive 
years of experience. On an average, women in Lebanon had owned their businesses 
for 10.6 years, in Bahrain for 10.2 years, in Tunisia for 8.6, in Jordan for 
6.1, and in the UAE for 5.9 years. Women business owners were actively involved 
in managing their enterprises and close to two-thirds spent at least 40 hours 
per week operating their businesses.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="news9.jpg" width="450" height="300"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<br>
The women business owners surveyed were largely optimistic and poised for 
growth, but in need of some direction and assistance to achieve their goals. 
Between 61 and 88 per cent of the participants expressed optimism regarding 
their own enterprises while 47-87 per cent expressed optimism about their 
national economic prospects. Many more businesswomen plan to expand their 
businesses rather than maintaining current levels of operations. Even in 
Lebanon, which has been facing turbulent times, the outlook was still very 
optimistic: 49 per cent are planning to grow, while 45 per cent have adopted a 
“wait and see” approach. Between one-quarter and one-half of women surveyed were 
trading internationally. Women business owners in all five surveyed countries 
used information and communication technology (ICT) for their businesses at 
rates well above the per capita average worldwide. <br>
<br>
The women entrepreneurs in different countries cited similar challenges in doing 
business, but with different rankings that varied depending on their specific 
environments. Among the top challenges faced by the majority of women surveyed 
were: learning financial management skills; finding and keeping good employees; 
access to capital; and, the high cost of public services. <br>
<br>
The other key challenges were the high cost of labour, the desire for access to 
general business training and support, and the importance of gaining access to 
new markets for their products and services.<br>
<br>
The full report is available on www.ifc.org.<br>
