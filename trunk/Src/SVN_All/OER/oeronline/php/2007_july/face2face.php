<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><b>
<font face="Verdana" size="4">
‘One must diversify to grow’</font></b><font face="Verdana" size="1"><br>
<b>A major event recently witnessed by the media industry in Oman was the launch of 
Hala FM radio station. The man behind this first-of-its-kind private initiative, 
Maqbool Hameed, Chairman, OHI Group, reveals the company’s future strategy for 
media business</b><br>
<br>
<b>
<img border="0" src="face.jpg" width="175" height="240" align="left" hspace="6" vspace="4">What attracted you to the world of media? What is your strategy for the FM radio 
business</b>? <br>
Earlier, whenever I heard the FM radio, I used to analyse the content and the 
commercials. It was this that gave me the idea of creating something that not 
only disseminated information but also made profit. Following up on this in 
1995, I submitted an application to set up a private radio station to the 
Minister of Information who promised to suitably consider it. On October 10, 
2005, we got the government go-ahead to set up two radio stations. Accordingly, 
we recently launched the new Hala FM. We plan to launch another FM station, 
airing mostly English content, by September. Hala FM will focus on commercials 
and cultural content. It will not provide political news. It should be noted 
that our motto is ‘real media and recreation’. Of course, we will do everything 
possible to support developments taking place in Oman. <br>
<br>
<b>What is going to be your strategy for the group’s media business?</b><br>
When we had applied to set up an FM station, we had also sought permission to 
set up a TV channel. We did receive the ministry’s approval for that also, but 
the licence lapsed as we failed to submit the comprehensive plan in the 
stipulated time. However, we do have a clear plan in the TV media and we will 
approach the ministry whenever fresh applications are allowed. In the print 
segment too, we had registered a title for publication with ministry. However, 
we did not pursue this further as we found that it was not commercially viable 
to enter this segment at this point of time. Similarly, we had approached 
another company that was seeking to sell its media business, but backed off as 
the price was not very attractive. So currently, we are only focusing on the FM 
business and have no immediate plans for either the TV or print segments.<br>
<br>
<b>How do you evaluate the investment scenario in Oman? </b> <br>
A few years ago, a prospective investor had said to me that there was a smell of 
good investment in Oman and now not only he smells it but also sees it. This 
holds true today, and the opportunities available are there for all to see. For 
example, US$16-billion worth of investments have been pumped in Sohar in the 
last few years. A number of gigantic tourism projects have also been set up. The 
ministries and public departments too are taking every possible measure to 
provide a healthy investment environment. I believe it is the right of every 
Omani to profit from such a favourable investment environment. I can confidently 
say that the Sultanate is rich for investments and there are loads of 
opportunities for serious investors and strong-hearted entrepreneurs. However, 
we need more incentives and support from the Omani government.<br>
<br>
<b>
<img border="0" src="face1.jpg" width="300" height="209" align="right" hspace="6" vspace="4">Specifically …?</b><br>
…family-owned companies should be encouraged to transform into joint stock 
companies. The regulatory bodies should allow the founders of such companies to 
own 70 per cent stake and not limit this to only 25 per cent, as this does not 
guarantee that the founders will remain the chairpersons.<br>
<br>
<b>What are the major challenges ahead of you? </b> <br>
Bureaucracy is one of the major challenges, because many officials do not really 
appreciate the real value of time or show the required flexibility. We hope that 
transactions with the government will be processed promptly.<br>
<br>
<b>How do you see the FTA impact on the Sultanate? </b> <br>
We have a number of advantages such as cheap and skilled manpower for investors. 
The tax rates in Oman are very low compared to the US and Europe. The FTA will 
help us in setting up new partnerships and open the doors to competition that 
will improve the quality of our products and ensure better price of goods.<br>
<br>
<b>Tell us about how you set up OHI? </b> <br>
I started my career at the age of 13 with the Omani army. My eight-year stint in 
the army taught me so many valuable values like dedication to work, diligence 
and punctuality, not to forget the importance of managing time. Subsequently, I 
worked for some years in Dubai, Abu Dhabi and Bahrain. One day, I listened to a 
report aired by the BBC on the progress made by the Sultanate of Oman after HM 
Sultan Qaboos’ accession. It was then that I decided to return to Oman and start 
my own venture. Starting from selling sanitary fixtures and importing Coca Cola 
from Dubai, I then sold Daihatsu cars and Yamaha motorcycles. It was a period 
when I was multi-tasking – as a clerk, salesman, accountant and what not. My 
efforts have borne fruit and today my holding company is listed on the Muscat 
Securities Market, with more than 2,000 shareholders.<br>
<br>
<b>What are the OHI Group’s investment plans? </b> <br>
Last year, we raised the group’s capital to RO8.5 million while our market value 
has been put at RO26 million. We have medium to long-term plans for expansion. 
For example, we have plans to set up the Centro Hotel in Sohar and another hotel 
in Al Khuwair. We also plan to open a new camp in Remal Al Sharqiya that will 
provide five-star services to desert lovers and tourists. <br>
<br>
<b>What about diversification? </b> <br>
I strongly believe in the adage – ‘don’t put all your eggs in one basket’. 
Diversification is thus essential for any business. If I concentrated on only 
one industry, I would not have been able to grow. In fact, at the company’s 
recent annual general meeting, I mentioned that today the group comprises more 
than 25 companies operating at various levels.<br>
<br>
<b>Are there any new partnerships or new companies in the pipeline? </b> <br>
We have material plans for partnership with one Indian-American company to 
produce potassium in Sohar. We had a partnership with Abdullah Al Naser Group 
for transformer manufacturing in Abu Dhabi. It allowed us to buy Oman Steel Co. 
Regarding new companies, we will take a call when the board of directors meets 
but we do have certain expansion plans and are just waiting for the right time 
to go ahead. </font></p>
