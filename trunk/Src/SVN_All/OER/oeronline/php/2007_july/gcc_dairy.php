        said: “Porta Reef Investment Fund is the second fund that Abu Dhabi 
        Investment House launched in two weeks. Earlier, Sunset Hills Fund was 
        launched in cooperation with Sorooh Investment Company. This fund is 
        expected to yield targeted annual return of 20 per cent over 24 months, 
        Al Jowder confirmed, “Porta Reef targets middle and high-income citizens 
        or residents in the Gulf.”<br>
