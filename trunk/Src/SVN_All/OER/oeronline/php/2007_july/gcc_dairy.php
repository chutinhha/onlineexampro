        <b>Kuwait investors to manage US$200-mn Indian fund</b><br>
        Bahrain-based Khaleej Finance and Investment (KFI) has joined hands with 
        Kuwaiti investors to manage a US$200-million Indian Private Equity Fund. 
        KFI, Kuwait Investment Company (KIC) and Kuwait Finance House (KFH), 
        also known as Baytak, have signed a partnership agreement to manage and 
        promote the Sharia-compliant fund, targeting a variety of activities 
        with controlled risks in growing sectors, particularly real estate and 
        industry in India.<br>
		<br>
        The fund is designed to reach an internal rate of return exceeding 25 
        per cent at the end of the investment period of five years. It has an 
        investment period ranging between three and five years and is partially 
        underwritten in Kuwait by Warba Investment Company.<br>
		<br>
        KFI Chief Executive Nabil Hadi, Baytak General Manager Mohammed al-Omar 
        and KIC chairman Badr al-Sbei said it represents a strategic step 
        forward for their institutions. “The Indian Private Equity fund 
        constitutes a qualitative change in the activities of KFI and is 
        designed to help the bank go beyond the regional level,” Hadi said.<br>
		<br>
        Talking on the partnership between KFI, KIC and KFH, Hadi said, “This 
        partnership comes as part of the bank’s strategic vision of building 
        alliances and strong long term relationships with banks and investment 
        institutions in the GCC countries.”<br>
		<br>
        KFH-Baytak General Manager Mohammed Al Omar said, “We found that Indian 
        Private Equity Fund has diversified the targeted sectors and distributed 
        the allocations in a way that satisfies investors and minimises 
        potential risks on their investments.”<br>
		<br>
        KIC Chairman Nasser Al Sbei announced that KIC and KFH are the 
        co-managers, co-promoters and co-placement agents of the Indian Private 
        Equity Fund, while KFI is the arranger, the lead manager, the 
        co-promoter and co-placement agent.<br>
