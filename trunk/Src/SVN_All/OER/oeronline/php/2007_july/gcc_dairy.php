        <b>Saudi plant secures US$3.44-bn financing</b><br>
        A consortium of Suez Energy International, Gulf Investment Corporation 
        and the Arabian Company for Water and Power Projects has completed 
        limited recourse financing for the Marafiq Independent Water and Power 
        Project (IWPP) in Jubail, Saudi Arabia.<br>
		<br>
        A syndicate of 29 international, regional and Saudi banks is providing 
        loans to the tune of US$3.44 billion to the project.<br>
		<br>
        The Marafiq IWPP is a 23-year BOOT (build, own, operate, transfer) 
        contract for a 2,750-megawatt combined cycle power station and a large 
        water desalination facility with a capacity of 800 million gallons per 
        day. The consortium owns 60 per cent of the project, with the remaining 
        40 per cent held by Marafiq, the Saudi Electricity Company and the 
        Public Investment Fund.<br>
		<br>
        The power and water output from the facility will be sold under a single 
        off-take contract to a wholly owned subsidiary of Marafiq. The EPC 
        contract was awarded to a consortium comprising General Electric, 
        Hyundai Heavy Industries and Societe Internationale de Dessalement. <br>
		<br>
        “This is the largest project finance deal ever signed in the power 
        sector so far. It is a clear demonstration of our strong commitment to 
        both Saudi Arabia and the Middle-East”, said Dirk Beeuwsaert, Suez 
        Energy International’s Chief Executive Officer. “The arrangement of this 
        combined conventional and Islamic financing providing over 80 per cent 
        of the funds for the project, is a major achievement for all the 
        sponsors.”<br>
		<br>
        The financing is in five parts – a 22-year loan of US$1.57 billion, an 
        export facility of US$645 million provided by the Korea Export Insurance 
        Corporation, an Islamic Ijara facility of US$600 million, an equity 
        bridge facility of $496 million, and a debt service reserve account 
        facility of US$130 million.<br>
		<br>
        The syndicate is led by BNP Paribas, Gulf International Bank and SAMBA 
        Financial Group, together with Bank Saudi Fransi, Arab National Bank, 
        Saudi British Bank, Saudi Hollandi Bank, Arab Petroleum Investments 
        Corporation (Apicorp), Bayerische LB, Bank Of Tokyo Mitsubishi – UFJ, 
        Calyon, Dexia Credit Local, DZ Bank, Fortis Bank, HSBC, ING Bank, KBC, 
        KfW, Mizuho, Natixis, Royal Bank of Scotland, Sumitomo Mitsui Banking 
        Corporation, Societe Generale, Standard Chartered Bank, West LB, Arab 
        Bank, Mashreq, Shinhan Bank, and Woori Global Markets Asia Limited.<br>
		<br>
        The Islamic facility is provided by three Saudi banks: Riyad Bank, Al 
        Rajhi Banking and Investment Corporation and National Commercial Bank. 
        The debt service reserve account facility is provided by Bahrain-based 
        Gulf International Bank.</font></p>
