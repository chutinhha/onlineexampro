        <b>Investment in chemicals rises</b><br>
        Arab Gulf countries invested about US$70 billion in chemical and 
        petrochemical industries in 2006, five per cent more than they did in 
        2005, a pan-Gulf organisation that monitors such projects has said. 
        Saudi Arabia accounted for nearly 63 per cent of total Gulf Cooperation 
        Council (GCC) investment made in petrochemical projects, according to 
        Qatar-based Gulf Organisation for Industrial Consulting (GOIC).<br>
		<br>
        The investment in the petrochemical sector was 59 per cent of about 
        US$118 billion the region spent on all industrial projects during the 
        year. The UAE’s investment in the sector was US$5.8 billion, which 
        represented 46 per cent of total investment in industrial projects. 
        Qatar was the second biggest GCC investor in petrochemical industries at 
        US$9.6 billion, followed by Kuwait with US$6.1 billion.<br>
		<br>
        GOIC said the average growth of Gulf investments in petrochemicals and 
        chemical industries between 2000 and 2006 was about five per cent. The 
        number of people employed in these industries increased 4.9 per cent to 
        163,134 last year compared with the previous year. Investment in 
        petrochemical facilities is driven by availability of cheap feedstock 
        and the region’s proximity to large markets of Asia such as China and 
        India. <br>
		<br>
        More than 30 million tonnes per year of ethylene capacity will be added 
        in the Middle East by 2012-13, according to London-based Chemical Week. 
        The new capacity is also likely to put pressure on global petrochemical 
        markets. Steam crackers coming onstream from 2008 in Kuwait, Qatar and 
        Saudi Arabia will cause an imbalance in worldwide supply and demand, 
        lowering petrochemical plant operating rates and leading to a softening 
        in prices, it quoted industry analysts as saying. Some projects may be 
        delayed or cancelled as engineering, procurement and construction costs 
        in the Middle East have inflated faster than other places. </font></p>
