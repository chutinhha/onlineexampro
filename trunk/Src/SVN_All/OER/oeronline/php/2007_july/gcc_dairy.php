        Pvt equity fundraising to reach US$25 bn<br>
		</font><b><font face="Verdana" size="1">Fundraising in the GCC area reached US$10 billion in 2006, accounting 
        for just under a third of the total US$33.2 billion raised across the 
        emerging markets, says a report</font></b></p>
		<p align="justify"><font face="Verdana" size="1">
		<img border="0" src="gcc.jpg" width="250" height="200" align="left" hspace="6" vspace="4">Private equity fundraising in the Middle East could reach US$25 billion 
        in the near future, according to a report, ‘The Impact of Private Equity 
        on the GCC’. Produced jointly by Ithmar Capital, a GCC-focused private 
        equity firm, and Dow Jones, the report states that in the GCC 
        “fundraising is exhibiting strong growth with 41 per cent of the funds 
        raised between 1994 and 2005 raised in the final year of this period, 
        and then this total of US$5.8 billion almost doubling in 2006.”<br>
		<br>
        Growth in the private equity markets in the GCC is being driven by rapid 
        economic growth and restructuring, high liquidity from record energy 
        prices, significant fiscal and trade surpluses and heightened interest 
        in the region from international firms looking to hedge investment risks 
        and compete globally, says the report. Nonetheless, it concludes that 
        the “region is still underdeveloped compared to other geographies and 
        accounts for only a small proportion of the region’s overall economic 
        activity.” The report then says: “Fundraising in the GCC area reached 
        US$10 billion in 2006, accounting for just under a third of the total 
        US$33.2 billion raised across the emerging markets of Asia, Eastern 
        Europe, Latin America and the MENA region.”<br>
		<br>
        The report points to some specific benefits from a growth of private 
        equity in the GCC including, improved economic diversification and 
        support for the existing positive liquidity climate as well as for 
        growth of a viable private sector. “GCC states have identified assets 
        valued at more than US$1 trillion for privatisation, and public 
        infrastructure accounted for more than 60 per cent of new funds raised 
        by regional private equity firms in 2006. Governments in the region are 
        also likely to look to external capital sources to address shortfalls in 
        infrastructure financing and growing pressure on public resources 
        attendant on the GCC’s rapid population growth,” says the report.<br>
		<br>
        However, there are several challenges to realising the potential of 
        private equity in the GCC, points out the report. The first is the 
        reluctance of family businesses to give up controlling stakes, thereby 
        limiting exit opportunities. The presence of inconsistent regional legal 
        and tax regimes is the second challenge with underdeveloped human 
        capital and management pools being the third. The fourth challenge is 
        strong investor interest in government-owned infrastructure, which 
        reinforces existing structures, restricts technology development and 
        limits diversification.<br>
