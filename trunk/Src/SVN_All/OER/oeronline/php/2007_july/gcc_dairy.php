        <b>Qatar-backed fund ups Sainsbury stake to 25 per cent</b><br>
        <img border="0" src="gcc8.jpg" width="250" height="166" align="right" hspace="6" vspace="4">Delta (Two), a Qatar-backed property investment fund, has raised its 
        stake in J Sainsbury Plc to 25 per cent by purchasing US$1.4 billion of 
        stock, making it the biggest shareholder. The fund acquired 7.1 per cent 
        of the UK’s third-largest supermarket chain for 595 pence a share, Delta 
        has said. Delta is run by Paul Taylor, the former chief executive 
        officer of Rotch Property Group, a company owned by the billionaire 
        Tchenguiz brothers. Robert Tchenguiz holds 5 per cent of London-based 
        Sainsbury.<br>
		<br>
        Shares of the retailer rose to the highest in at least 18 years, giving 
        the company a market value of more than £10 billion. Sainsbury, whose 
        £8.6 billion of real estate has lured potential bidders, avoided being 
        taken over by CVC Capital Partners Ltd in April and said it won’t be 
        pressured into disposals. Delta’s stake is now larger than that of the 
        Sainsbury family, which owns about 18 per cent. “These guys will want a 
        say in what happens at the company and will want a better balance 
        sheet,’’ said James Bevan, who helps manage US$8.9 billion as Chief 
        Investment Officer at CCLA Investment Management. “We shouldn’t assume 
        that just because companies take big stakes they have predatory 
        intentions. They may just want a bigger say in how the company is run.”<br>
		<br>
        UK retailers and pub owners have come under pressure to separate 
        operating and real estate units since a new law came into effect this 
        year, allowing property divisions to trade separately as real estate 
        investment trusts (REITs), which qualify for lower taxes. CVC, lured by 
        the retailer’s real estate, failed to buy Sainsbury for £10 billion 
        after it was abandoned by its bidding partners and the grocery chain’s 
        founding family held out for more money. Sainsbury said in March it has 
        “right to retain ownership” of the land and that while it has considered 
        opportunities to raise money from its property, “now is not the time’’.
