        machines in remote areas of the Sultanate. Through mobile data 
        connectivity, Oman Mobile was able to connect these ATMs to the Oman 
        Arab Bank central computer system using a mobile data connectivity 
        solution, using wireless technology. The first experimental trial was 
        carried out in collaboration last month and is now implemented 
        commercially for the public. The partnership has enabled Oman Arab 
        Bank’s ATMs to operate using this unique mobile data connectivity 
        solution. The technology provides increased speed and flexibility in 
        activating an automated teller, since it dispenses with the network 
        cables required in existing systems. The solution makes it possible to 
        install ATMs in promotional activities and events as the mobility and 
        flexibility of being able to reposition the machines with wireless is a 
        real attraction. <br>
