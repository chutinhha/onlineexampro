<p align="justify">
<b><font face="Verdana" size="4"><br>
Shocks and pulls of inflation</font></b><font face="Verdana" size="1"><br>
<b>Increasing workforce numbers may be crucial to addressing the creeping inflation 
in Oman, warns Jason J. Nash</b><br>
<br>
<img border="0" src="economy.jpg" width="161" height="455" align="right" hspace="6" vspace="4">Historically, Oman has had a very low rate of inflation, and even deflation at 
times. However, over the past three years, inflation has started to increase, 
for reasons of supply shock and demand pull. Government statistics show that in 
the first quarter of 2007, consumer prices in Muscat grew 3.8 per cent 
year-on-year, a trend that looks to continue, as demand stays high and the 
dollar, and therefore, the riyal low. The rate of inflation in Oman has 
increased from 0.7 per cent in 2004 to 1.9 per cent in 2005, to 3.2 per cent in 
2006, and looks set to hit 3.8 per cent in 2007.<br>
<br>
For the past three years or more, all Gulf Cooperation Council (GCC) states have 
been witnessing increasing levels of inflation as they enjoy the fruits of the 
oil boom. A survey conducted by the Standard Chartered Bank in the Middle East 
region indicated the following three reasons behind GCC inflation: accommodation 
cost increases in certain countries; high liquidity; and the weakening of the US 
dollar against other major currencies. To this, in the case of Oman, one can add 
labour shortages.<br>
<br>
Strong and growing demand, driven by Oman’s 6.6 per cent growth rate and the 
official wage policy, has been contributing to growing inflation levels. On the 
supply side, movements between the US dollar and the euro/yen have been 
devaluing local currencies that are pegged to the dollar and, therefore, also 
contributing to the acceleration of inflation by increasing the cost of goods 
imported from Europe and Japan. Supply shock also comes from the shortage of 
skilled labour.<br>
<br>
M2, the overall sum of cash held by public, local currency demand deposits, 
savings and time deposits, and resident foreign currency, grew 21.3 per cent in 
2005 and 24.6 per cent in 2006, the highest since the 1997 boom. The growth in 
liquidity is due in no small part to the growth of corporate earnings of the 124 
companies listed on the Muscat Securities Market, which saw net profits grow 
10.4 per cent in 2006. Also, boosting money supply was the extra month’s salary 
awarded to public sector workers at the end of the previous year.<br>
<br>
Demand has also been boosted by the government’s move to increase public sector 
wages and raise the minimum wage for Omanis employed by state ministries and 
enterprises.<br>
<br>
On the supply side, the pegging of the riyal to the US dollar and the high level 
of imports (and therefore imported inflation) mean that the value of the dollar 
and global inflationary trends have a large influence on the level of inflation 
in Oman. The dollar’s weakness was the reason for Kuwait’s recent decision to 
end the dinar’s peg with the greenback, and instead peg it to a basket of 
currencies. By pegging to a basket, rather than a single currency, the influence 
of the low dollar exchange rate on the home currency is diluted. The Kuwaiti 
move had an instant effect, the dinar appreciated vis-à-vis the dollar. Imported 
inflation is seen as a direct result of the US dollar peg GCC currencies have 
maintained in their search to establish a
unified currency.<br>
<br>
Some other countries in the Gulf are also now reconsidering their rate peg. Such 
an assumption is not an overestimation, specially seeing how the dollar has been 
falling against other currencies like the euro and sterling. Maintaining the peg 
can be costly, as the cases of the UAE and Qatar demonstrate. The former 
recorded an inflation rate as high as 10.1 per cent at the end of 2006, while 
Qatar’s soared to 11.8 per cent. Kuwait’s decision to abandon the US dollar peg 
is just one of the examples of how GCC region economies are seeking to tackle 
the so-called “imported inflation” syndrome. <br>
<br>
However, Oman will almost certainly not go down the same road. In early 2007, 
the country withdrew from the GCC monetary union to pursue its own monetary 
policy. With the falling value of the US dollar and consequently weakening 
riyal, Oman has enjoyed a boost in its exports (which are priced in dollars) and 
this has turned the Sultanate into a more attractive spot for international 
tourists who benefit from the good exchange rate. Current inflation rate remains 
low enough to be seen as a reasonable price to be paid for such benefits.<br>
<br>
But a supply shock factor in Oman is that the private sector is seeing very high 
organic average wage growth, with the oil and gas industries seeing salaries 
increase by as much as 50 per cent for skilled labour. Human resource shortages 
are not a new phenomenon for the Sultanate; they have been an issue for 35 
years. However, the current increases in wages are unprecedented in scale, 
driven by high competition for skilled workers from multinational companies and 
the emigration of Omanis to other GCC member states with
higher wages. <br>
<br>
In this case, Oman is something of a victim of its own success. A member of the 
World Trade Organisation (WTO) since 2000, Oman signed a free trade agreement 
(FTA) with the US last year and the government has been actively promoting 
foreign investment. Foreign firms entering the market, or wanting to expand in 
it, need skilled staff, and if they do not have the time or resources to train 
them, they seek out their competitors’ employees. To win over, or indeed retain 
staff in this environment, remuneration packages have to be attractive. The 
policy of Omanisation, which sets quotas of Omanis as part of the workforce for 
organisations, also lessens companies’ ability to bring in further skilled 
workers from abroad.<br>
<br>
But, ironically, this is not the only reason for spiralling wage costs. Another 
reason for wage inflation in Oman is the higher salary increases in neighbouring 
countries, which draws Omanis to emigrate, decreasing the labour pool in their 
home country.<br>
<br>
Prices, however, continue to be restrained by the government’s extensive system 
of subsidies. While they distort the market, they are understandably popular 
with citizens and are unlikely to be scrapped in the near future. Indeed, in 
some areas, they are being extended. In May, Dr Khamis bin Mubarak Al-Alawi, 
Minister of Housing, Electricity and Water, announced an increase in housing 
subsidies to those eligible from RO10,000 to RO15,000 (US$25,970 to US$38,960). 
Surveys are already underway as the first step to construct a new database of 
citizens who can claim the money.<br>
<br>
Dr Al-Alawi explained that the extra cash was to help cover the increase in 
construction material prices and the escalating cost of building on difficult 
terrain. This should help restrain prices in the real estate sector, which has 
been the largest underlying cause of inflation in some of Oman’s neighbours, 
including Saudi Arabia. However, these subsidies distort the economy and should 
not be seen as a long-term fix. The supply side needs to meet the demands of the 
population, which is growing at 3.23 per cent annually and becoming ever more 
wealthy, and is more likely to live separately from other family members.<br>
<br>
Oman’s inflation is relatively low for an emerging market, and is below many of 
its GCC member neighbours. Much of the inflation growth has been due to the 
booming market, which has increased liquidity and drawn in labour-hungry 
employers. As Oman will be reluctant to depeg from the dollar due to the 
benefits it reaps from the arrangement, in the medium-to-long term, one of the 
best tools for tackling inflation will be an increase in the skilled labour 
pool. While subsidies in the real estate sector ease the pain in the short term, 
over the coming years, Oman will need to increase the housing stock to cater for 
its growing, ever-richer population. Equally, the government should be careful 
in growing the money supply, as excessive spending could feed an inflationary 
cycle that will eat up the much-desired economic growth. Though economic 
populism can be tempting in the present revenue boom, careful spending could see 
the benefits of the boom last longer and help Oman overcome the “boom-bust” 
cycle that many of its neighbours could enter into if not vigilant. <br>
