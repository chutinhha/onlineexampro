</font><b><font face="Verdana" size="4">Far from blown away</font></b><br>
<font face="Verdana" size="1"><b>The MSM seems to have tided over the storm and it’s back to business with 
investors focusing on long-term prospects<br>
</b></font>
<br>
<font face="Verdana" size="1">Raging bulls in the market, then a cyclone that shook the country like never 
before, a phase of uncertainty following, and then back to the bull phase again! 
16 May-15 June 2007 was anything but dull for the Muscat Securities Market. The 
benchmark MSM-30 index gained 336 points, or 5.6 per cent, during May 16 to June 
15, propped up mainly by the banking and industrial indices. However, market 
activity as measured by total turnover declined by almost 21 per cent to RO156.5 
million, mainly due to the impact of the cyclone and its after effects. It was a 
time of uncertainty and anguish, and concerns increased when the market did not 
open for trading as scheduled on June 10.<br>
<br>
Opening the next day, Monday, June 11, the MSM was caught by a strong negative 
bias, with broad-based selling in most large counters and the MSM30 Index ended 
3.13 per cent down, managing to stay just above the 6,000 mark. But, most 
institutional investors were of the belief that this was only a temporary blip; 
many, in fact, hinted that they would step up buying if the downtrend continued. 
The market rebounded the very next day, recovering almost 80 per cent of its 
previous day’s losses.<br>
<br>
Corporate-wise, the second half of May witnessed an unusual tug of war between 
the boards of BankMuscat and Alliance Housing Bank (AHB), with the former 
convening an investor meet to explain its rationale for making an offer for AHB 
stock at RO0.375 per share. Buying in two index heavyweights – Omantel and 
BankMuscat – propped up the MSM-30 Index. Omantel announced that it was 
considering a stake acquisition in a major Pakistani telecom company.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="market.jpg" width="493" height="638"><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">MSM has been witnessing a strong upward momentum after the posting of results 
for Q1 2007. Even after the re-opening post the cyclone, the market was back on 
its feet, albeit after a day’s panic, suggesting that investors continue to bet 
favourably on the long-term prospects of local companies. Though retail 
participation was affected by the cyclone, institutional activity is expected to 
remain firm. Summer volumes are generally lower than at other times of the year, 
and this coupled with lesser retail interest could adversely affect
market turnover. <br>
