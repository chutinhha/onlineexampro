        <p align="justify"><font face="Verdana" size="2"><b>Paens to Private 
        Initiative</b><br>
        <br>
        <img border="0" src="cover_eng1.jpg" align="left" width="150" height="187" hspace="6">Rome 
        was not built in a single day. It took ages of meticulous and 
        painstaking efforts to construct the Roman empire which later came to be 
        viewed as one of the most revered civilizations. Modern day Oman, the 
        one that we are witnessing today, is again not something that was bought 
        off the shelf sans sweat. <br>
        <br>
        Did we not hear His Majesty Sultan Qaboos bin Said on the first National 
        Day celebrations way back in 1971 wherein he had said without mincing 
        words: &quot;Building process is an exacting one and that much hard work and 
        sacrifice is needed to overcome the obstacles and difficulties before 
        us?&quot; <br>
        <br>
        A nation that has a trading history running into several centuries has 
        its own issues. His Majesty was fully aware of this predicament. 
        Naturally, it had befallen on the state to mount a massive nation 
        building programme to create a modern state as he has envisaged. <br>
        <br>
        No sooner than did His Majesty took charge, one of the first things he 
        did was to assemble a group of visionaries to chalk out the roadmap for 
        the Sultanate of Oman. Sensing the big opportunity, young Omani 
        entrepreneurs moved in to help build a brand new Sultanate and in the 
        process to build business empires for themselves. <br>
        <br>
        Leaders, it is said, cannot have leaden feet. They have to be 
        nimble-footed: ready to change gear to take on new challenges. His 
        Majesty, like a true visionary and the ablest leader was firmly planted 
        in the driver’s seat. Sensing that the State has its own limitations, he 
        addressed the Omanis in 1995 with: &quot;Let us see what happened to some 
        countries where the citizen depended wholly on the State. Those 
        countries collapsed and poverty and instability prevailed there. No 
        country in the world can revoke the role of the individual nor can the 
        individual depend wholly on the State&quot;. True words, indeed! <br>
        <br>
        His basic idea was to throw open greater opportunities for private 
        sector. His Majesty pointed out, &quot;the private sector in all countries 
        now represent the backbone of their economy. This is so because it is 
        capable of both determination and competition and offers a solid base 
        for the government and the people.&quot; <br>
        <br>
        It was not an easy ride for His Majesty and he was open about it. &quot;Some 
        small minds have asked why the Government should spend so much in 
        subsidies on businessmen and company owners and provide them with the 
        facilities. I help the private sector so that it can provide jobs for 
        the citizens, benefit the community and strengthen the national 
        economy.&quot; The role of private sector – both home grown and foreign 
        companies – in building the present day Oman is truly laudable. That is 
        the reason, why OMAN 1970 - 2006 chose the occasion of National Day 
        falling on November 18 to salute those business enterprises – big and 
        family owned, multi- national ventures, medium-sized, crown jewels– that 
        put their collective heart and soul in translating His Majesty’s grand 
        vision into reality. <br>
        <br>
        His Majesty is clear and focused on preparing Oman for the 21st century 
        through strategic evolution. Oman’s economy will no longer be dependent 
        on Government expenditure, oil resources and foreign workers, but it 
        &quot;will enter a better and more comprehensive phase, based on private 
        initiative, nationally trained workers and sustained established 
        resources&quot;. The emphasis, needless to add, is on private initiative. The 
        private sector has not let down the Sultanate in the past. They are 
        unlikely in the future. Such is their glorious past record. More power 
        to them!<br>
        <br>
        The guest writers in this book are all experts in their respective 
        fields and are well known and respected individuals who have been and 
        still are a part of this progress and change. We thank them for their 
        generosity and spirit and for sharing their thoughts and knowledge with 
        us. <br>
        <br>
        It is an honour to present this book to you. We realise that it will 
        greatly illuminate your national pride and reinforce the feeling that 
        Oman not only stands out in the region, but that its brilliance reaches 
        the far corners of the world!</font><br>
