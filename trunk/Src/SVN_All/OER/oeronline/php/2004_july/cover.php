            <p align="justify"><font face="Verdana" size="1">This is, again, easier said than done. Agrees Hobbs: &quot;Business is 
            all about making a profit and giving a service. However, at times, 
            there tends to be a little bit too much of the 'making profits' part 
            of it.'' Counters Saher: &quot;All you need to achieve that objective is 
            someone to step outside the crowd and say that I will follow the 
            value system, certain right ways of doing things, not taking 
            short-cuts, not indulging in practices that are not right... In 
            fact, you need many such business leaders to start believing in it. 
            This is a big challenge anywhere in the world today, and should be 
            built into the national strategies - whether into the curriculum in 
            the schools or the code of ethics of private and government 
            organisations... Someone has to bell the cat. If people are talking 
            in terms of setting visions and missions and going after creation of 
            excellent world-class companies, these things are equally 
            important.'' <br>
            <br>
            <img border="0" src="john.jpg" align="right" width="156" height="201" hspace="6"><b>Decisions CEOs take</b> <br>
            The role of the CEO may have become a lot 
            more challenging, but it is no way any less interesting than what it 
            was earlier. In the face of turbulent times where speed and 
            flexibility are essential, CEOs need to be ambidextrous. Personal 
            characteristics often play an important role in the unpredictable 
            career trajectories of a CEO. A typical day in office for a CEO is 
            extremely unpredictable, and that's one of the great attractions of 
            the job, according to Hobbs. <br>
            <br>
            One moment the CEO could be looking at taking existential decisions 
            like entering a new market or introducing a product line, and he 
            could well be spending the next hour trying to explain to the 
            support staff the importance of maintaining hygiene in the 
            workplace. &quot;You will no doubt be surprised to know there is a large 
            disparity in the complexity of the decisions that my office is 
            confronted with on a dally basis. For example, in any one day I 
            could be addressing a strategic decision, to considering 
            participation of a regional syndication and its effects on our 
            mismatch to dealing with a staff request that is an exception to 
            policy, to responding to a regulatory issue. There are strategic 
            issues to be addressed every single day. This variety and change in 
            complexity is what makes this job so fulfilling, and continues to 
            challenge my flexibility, decision-making skills and the management 
            of my time,'' confides Carlough. <br>
            <br>
            &quot;Quite often, you have six meetings in a day - you have regular 
            meetings to keep the team motivated and going in the right 
            direction; you have some meetings that are about new innovations; 
            some meetings about long-term strategy; lots of coordination with 
            our airlines, D-G Cam, the police, immigration etc.,'' echoes Hobbs. 
            Saher, on the other hand, tries to restrict himself to taking 
            strategic, non-operational kinds of decisions. &quot;Mainly, my decisions 
            are strategic in nature, concerning the direction, the strategy, the 
            resources, decisions on new business (go/no-go) and the structuring 
            of the organisation. We have delegated fair amount of authority to 
            the general managers of the various divisions of our company. So, 
            the decisions that I take end up being more strategic. <br>
            <br>
            However, he too doesn't enjoy the luxury of not taking routine 
            operational decisions when the need arises. &quot;I also do get involved 
            operationally when we have to take a decision which is an exception 
            to the set authorities. We do have policies on most things, 
            including a very well-documented personnel policy, but life is such 
            that there seem to be too many exceptions. When people want to come 
            up with an exceptional request, it invariably gets escalated because 
            it becomes a policy issue and one has to take a stand. That's 
            another area where I take decisions,'' he admits. <br>
            <br>
            Lawati, however, manages to the art of deft delegation to the T, and 
            gets involved mainly &quot;in top level decision-making on 
            policy/procedural matters, business strategy direction, business 
            performance appraisals, investment proposals, corporate matters and 
            a host of other non-routine business aspects.'' In SOMC, he claims, 
            &quot;we have a very clearly defined authority structure that enables our 
            managers to make business decisions (up to defined limits) without 
            necessarily referring all matters to the top management; an 
            effective delegation process that provides authority to the people 
            of the organisation in the right manner and makes them responsible 
            is key to a successful organisation. This helps the smooth running 
            of the organisational process in an efficient and controlled 
            manner,'' he adds. <br>
            <br>
            One of the skills of being a CEO is trying to put energy--but not 
            too much--into things that matter. &quot;You don't interfere with the 
            professionals in their job,'' maintains Hobbs, &quot;but I try and make 
            sure that they have enough steering, enough guidance so that they 
            know where they are going, and if they have a crisis, I pitch in and 
            help them just for 10 minutes or an hour or a day or whatever it 
            might need, and then I back off again. It's a tight-rope all day 
            long.'' Khimji too keeps himself busy with taking strategic 
            decisions about how sales can be improved or how to reach out to 
            more countries, increase brand awareness, and enhance the product in 
            the best possible way. &quot;There are always value-additions that can be 
            made, to bring in variety or to ensure that the product is 
            well-accepted in the various markets that it is exported to,'' he 
            suggests. <br>
            <br>
            Taking further his analogy of the CEO as the conductor of an 
            orchestra, Hobbs explains that &quot;like the conductor of the music, he 
            has to sense which bits are coming to the fore at any one time and 
            ensure that they are in harmony with the rest of the notes. It might 
            be, for instance, maintenance--we may have a problem with 
            toilets--so, I need to put my time into maintenance. It may be 
            safety issues--we may have rubber that needs to be taken off the 
            runway--so I put my attention on to that. So, from day-to-day, I 
            will have different things that come to the top of my list that are 
            priorities that I must attend to first of all.'' <br>
            <br>
            The other part of the CEO's role is, of course, long-term. It's a 
            combination of determining strategy and knowing where you are going 
            in the future and letting the entire team know that. Above all, it 
            includes building the motivation of targeting the common knowledge 
            of where everybody is heading so that the entire team pulls in the 
            same direction. Whatever the end objective might be, the change 
            constant ensures that the role of our business leaders continues to 
            be demanding as well as interesting at the same time. As Hobbs sums 
            it up: &quot;Things happen quite often. Harold Macmillan, former PM of 
            Britain, was asked what was the single greatest influence on his 
            success as a prime minister - 'Events, dear boy, events.' In other 
            words, you never know what's going to come out and how you are going 
            to handle it. That makes the job interesting.'' <br>
            <br>
            <b>Things CEOs do</b> <br>
            There is no 'typical day' in the life of a CEO 
            - which is probably what makes his job all the more compelling. As 
            mentioned earlier, the role of a CEO is such that it demands the 
            individual to be different things to different people at different 
            times in different scenarios. For a CEO, there is no way to explain 
            accurately and succinctly just what it is that he strives to 
            accomplish on a daily basis except for, perhaps, continuous 
            organisational improvement. <br>
            <br>
            Articulates Saher: &quot;We are so diverse that a typical day involves 
            completely different activities - I could be dealing with the 
            marketing strategy in the morning, the next hour I could be spending 
            on an HRD related issue, the next hour I could be spending on a new 
            project that we are setting up, then I may be looking into statutory 
            issues concerning accounts and audit report - tax planning. The 
            biggest challenge--one I enjoy too--is the diversity of my job and 
            the versatility that I have to bring to play to be able to 
            completely unrelated issues.'' <br>
            <br>
            Carlough's day starts with a brief overview of the local newspaper 
            to keep abreast of the local and regional events, market changes and 
            business opportunities. &quot;Of course, I follow the share prices - the 
            banking sector in particular,'' he says. &quot;Throughout the week, I 
            have scheduled morning meetings with different direct reports to 
            discuss results, target to plan variances, and a detailed list of 
            their pending business issues,'' he reveals. &quot;On a monthly basis, we 
            review their accomplishments against planned Management by 
            Objectives (MBOs) for the month, and discuss any shortfalls and look 
            for fixes. Nearly every day there is a customer or vendor visitor,'' 
            he adds. <br>
            <br>
            &quot;There is a tremendous volume of reading material that comes across 
            my desk everyday such as reports, requests of action, strategic 
            documents and approval documents. This activity demands a large 
            percentage of my day and requires timely response from my office in 
            order to maintain continued improvement of the organisation,'' he 
            adds. Carlough also attempts to make time to visit some department 
            of the head office to interact with the support staff there on a 
            daily basis. &quot;While not daily, I do spend a fair amount of my time 
            in the field, visiting branches, a part of my job that I thoroughly 
            enjoy and find most helpful when planning future strategies.'' <br>
            <br>
            A major part of Khimji's day, too, is occupied by &quot;meetings, 
            inspections, and being with the sales force.'' On a similar note, 
            Saher tries to rally the team around a common cause through regular 
            meetings. &quot;My style is one of discussion and debate. So, we do have 
            a lot of meetings. My typical day would include four or five 
            meetings, two or three external, and the rest internal. All our 
            systems are completely wired, so I do get what I call a top view in 
            terms of the MIS. I make it a point to keep an eye on the 
            consolidated figures that emerge. I also spend half-an-hour to an 
            hour with my chairman and directors to discuss issues that they'd 
            like to seek my ideas/opinions on.'' <br>
            <br>
            His endeavour, though, is to have 15 to 20 minutes a day to himself 
            when he is actually not doing anything, but reflecting on various 
            issues. &quot;Unfortunately, I am not able to do that on most days as, 
            more often than not, the urgent takes over the important,'' he 
            laments. <br>
            <br>
            Lawati's typical day in office entails &quot;engagements with people 
            (staff/visitors), tackling my ever growing email inbox, dealing with 
            paperwork, review of various data, reading daily papers, and time 
            for brief reflection before I make major decisions. Once or twice a 
            week, there would be an outside office task to attend a meeting, 
            participate in a function or a late evening party.'' <br>
            <br>
            <img border="0" src="mse1.jpg" align="left" hspace="6" width="150" height="198"><b>Games CEOs play</b>
            <br>
            So we all agree that from boardroom boredom 
            to jetlag jitters to stock market shocks, the country's business 
            barons lead a high-strung life. How, then, do they rejuvenate their 
            body and soul? How do they ensure that, after a hectic day's 
            work--meetings, inspections, strategic decisions etc. etc.--they are 
            ready for the grind the next day? The job of a CEO/leader is very 
            demanding and stressful. Managers need a clear understanding of what 
            creates stress and how to deal with it effectively. The three parts 
            of the managing stress triangle are: l Demands l Coping skills l 
            Reactions <br>
            <br>
            <b>Demands:</b> CEOs face a variety of demands from work, family, 
            and self-imposed standards. Among other things, work demands include 
            heavy workload, tight deadlines, travel, demanding customers, upset 
            employees and personality conflicts. The workplace holds a plethora 
            of anxiety producers. Family demands include holidays and special 
            events, family conflicts, elder care, spending time with spouse or 
            significant other, spending time with the kids, and other household 
            chores like paying bills and weekend shopping. Self imposed 
            standards could include 'perfection', must dos and the likes. The 
            number of demands and their importance produces an overall 
            cumulative effect. <br>
            <br>
            <b>Coping skills:</b> This refers to the skills and techniques 
            people use to deal with life's demands. They include 
            management/interpersonal skills (goal-setting, prioritising, time 
            management, delegation etc.), emotional intelligence (managing one's 
            feelings and awareness), and relaxation techniques (stress and anger 
            management). <br>
            <br>
            <b>Reactions:</b> The number and severity of the demands managers 
            face and the level of their coping skills combine to produce a wide 
            range of reactions. For example, a person with excellent coping 
            skills facing moderate demands will get the job done with little to 
            no stress. On the other hand, a person with average coping skills 
            facing difficult demands will experience a great amount of stress. 
            Each individual has his own way of unwinding - working out, playing 
            golf, indulging in a hobby. The best managers know how to handle 
            more demands while maintaining low stress levels. Developing 
            effective coping skills is critical. What do our business leaders do 
            and why? <br>
            <br>
            Avers Lawati: &quot;I resort to tennis for physical exercise, which I 
            have been doing for the past 10-15 years. Lately, it had become 
            difficult to find time for it, but recently, I have been able to 
            devote an hour about three times a week for what I call 'sweating 
            sessions' on tennis courts at the PDO Recreation Centre. It's good 
            fun and good exercise. I also like reading philosophical, religious 
            and poetry books, for which I usually dedicate the last half hour 
            before I go to bed.'' <br>
            <br>
            &quot;I like to go for a swim once in a while, at times in the sea and at 
            others in a swimming pool. I also like touring in the interior of 
            Oman to visit historical places such as old villages, forts and 
            castles, and some of the key valleys and hill stations - 
            Misfat-al-Abriyeen is my favourite, and I make it a point to use the 
            opportunity of visitors (both business and private) to take them 
            around. Chess is another hobby of mine, which has been lately taking 
            a back seat waiting for the right partner and enough time,'' he 
            adds. <br>
            <br>
            And how do these leisure pursuits help him in recharging himself for 
            the grind? &quot;Diverting focus to something 'different' is always 
            useful. I feel quite happy with the balance and diversity in my 
            life, which allows me to have the level of intensity and focus that 
            my job needs. As a believing Muslim, it is also important to mention 
            that I find my life very organised and well-directed with the right 
            place defined for 'work' in a person's life. The only other thing to 
            add would be my (private) social activity in form of a weekly 
            get-together with friends and relatives, without which I would have 
            missed a lot.'' <br>
            <br>
            Khimji, on the other hand, is an avid practitioner of the Art of 
            Living (AOL), a voluntary, non-profitable organisation recognised by 
            the UN, that conducts personality development workshops and is 
            committed to developing 'full human potential and fostering human 
            values in the global society'. &quot;Besides being a volunteer, I look 
            after the organisation's activities in other countries in the GCC 
            and the Middle East. I am a member of the apex body of AOL and have 
            become an instructor since January this year,'' he informs. <br>
            <br>
            <img border="0" src="kr.jpg" hspace="5" align="right" width="200" height="156">Khimji practises his AOL breathing regimen daily without fail and 
            does a bit of meditation too. &quot;I am enjoying it - at the end of the 
            day, it brings a lot of joy and peace, and I feel really happy about 
            it. It keeps me busy after work. AOL has brought a lot of change in 
            my life,'' he maintains. Attending the AOL workshop, according to 
            Khimji, &quot;has brought a smile to my face. Earlier, I used to have a 
            lot of worries. I seldom used to smile. Attending the course has 
            helped me start accepting things as they are, and I have started 
            handling situations in a better way, which made me worry less,'' he 
            claims. <br>
            <br>
            &quot;Play, that's how I rejuvenate myself,'' answers Carlough. &quot;We try 
            to walk on the beach or in the neighbourhood for a minimum 1 hour at 
            least four times a week. On long weekends, rare as they are, we 
            attempt to go diving. I also do a lot of camping during the winter 
            months and enjoy the wonderful hospitality of the people in the 
            interior. I have also been known to throw a tent up on the beach, 
            light a nice big fire and sit back and enjoy the great outdoors of 
            the wonderful Omani evening skies. Very relaxing! I must admit, 
            though, the best place I have found for my wife and I to rejuvenate 
            ourselves is under a palm tree at the local club, sitting in the 
            shade, reading and relaxing. Oh yes, I must admit I also go to a 
            nearby ayurveda clinic once a week for a relaxing massage. It really 
            works!'' he declares. <br>
            <br>
            &quot;I must admit that I spend a fair amount of time online with my 
            laptop keeping up to date with family at home, shopping and 
            generally using the Internet as a great source of information,'' he 
            adds. &quot;I've always been a 'water person', living and playing in 
            oceans, lakes and rivers; so, I continue to stay 'wet' here in Oman. 
            Some of the best beaches and coves I've ever seen are here In Oman. 
            I also enjoy photography and shoot whatever is available when I have 
            some free time. I've taken hundreds and hundreds of pictures of Oman 
            since I've been here and will continue to add to my collection. I've 
            been to the camel races, horse races, witnessed villagers dancing, 
            been up the wadis during rain storms, experienced the beauty of the 
            Khareef and have captured it all in my digital camera. I wish I had 
            more time for such activities. I need to take a vacation here In 
            Oman! Enjoyment is my battery re-charger,'' he adds. <br>
            <br>
            &quot;One thing I realise at this stage of my life is that my most 
            valuable asset is my time, and I am very conscious about it. I make 
            sure that I do my walk regularly to keep my body in shape. I am fond 
            of self-awareness and personal growth related endeavours. At this 
            stage of my life, I am very concerned about whether I am personally 
            growing, i.e., am I able to deal with my self, my ego, my reactions 
            to issues the way I would like it to be or is it, at most times, out 
            of my control. I generally would like to witness my own self in 
            terms of how am I reacting, how am I behaving, what am I saying... 
            So I do a lot of these relaxations, meditations. They act as a 
            release and definitely help to be in touch with oneself. I do get 
            into this mode every now and then,'' divulges Saher. <br>
            <br>
            &quot;Before I came to Oman, I really did not have time for my family. 
            Oman has certainly given that tranquillity that I am able to spend 
            some amount of quality time with my family. I also play the keyboard 
            - just as fun. I never learnt it, but play it once in a while as a 
            way of letting my hair down. I also do my reading to make sure that 
            I am in touch with reality,'' he adds. <br>
            <br>
            Hobbs, a self-confessed dabbler, indulges in a lot of hobbies, but 
            pursues none too deeply. &quot;Firstly, I try to keep fit by going to the 
            gym thrice a week. I like the outdoors - I love the sunshine. But I 
            am also very keen on humour and music. I have a lot of music 
            instruments at home but I am very bad at them. My favourite 
            instrument is a banjo. I also play the guitar and keyboards. And if 
            I was in England, I'd be doing a lot more practical things like 
            building furniture and stuff. I am also into astronomy. And, of 
            course, exploring - we have a lot of good mountains around here in 
            Oman. I also like sailing.'' <br>
            <br>
            These activities, according to Hobbs, help him bounce back every 
            time he is down. &quot;I can get really down. I can go home at the end of 
            a particularly bad day and really never want to see this place (his 
            office) again. But somehow, you wake up the next morning and you are 
            back in here and you are bouncing back and you put whatever happened 
            yesterday behind you or at least try and do something about it. It's 
            no good dwelling on the downside - you just got to keep on going,'' 
            he says cheerfully. <br>
            <br>
            &quot;I love jokes because they kind of give you a mental refresher. It's 
            all quite calculated really - you run the risk of getting 
            overstressed by concentrating too much on something. At times, you 
            have to do that - but at the first opportunity you get, send the 
            mind off in a different direction - whether it be astronomy or music 
            or a silly joke or anything. That just kind of gives you a bit of 
            refresher and then you go back into it and start all over.'' <br>
            <br>
            In conclusion, the CEO today has an unenviable task of effectively 
            managing shareholder expectations about the bottom line during an 
            economic downturn. The smart CEO today is choosing what not to 
            do--which jobs and offices to cut, which deals to scrap, which 
            products to stop--as well as looking to the future; which risks to 
            take, bargains to buy and innovations to make. Above all else, CEOs 
            need to keep one eye on tomorrow and create that vision for the 
            future.<br>
            <br>
            An effective CEO, therefore, looks to create relevance for every 
            audience, to balance the needs of the financial market with those of 
            employees and the societies in which the company operates. This 
            involves integrating corporate reputation very closely into business 
            strategy, and recognising that he makes a statement through his or 
            her values and behaviour. Today's CEO, operating in this Age of 
            Uncertainty, needs to be truth-teller, business leader, global 
            statesman, and above all else, confidence builder.</font></p>
