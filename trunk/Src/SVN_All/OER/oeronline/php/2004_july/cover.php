            <br>
            </font><font size="4" face="Times New Roman">W</font><font face="Verdana" size="1">hat is a CEO's job all about? Does his responsibility begin and end 
            with running his corporation 'successfully'? Maybe not. Do the 
            country's chief executives, or, for that matter, the chairmen, 
            managing directors and even group general managers--those at the 
            helm of affairs of corporate Oman--believe so? Or have they lulled 
            themselves into a false sense of well-being by convincing themselves 
            that they're doing a great service to all by running their 
            corporations with clinical precision, not making time for 'larger 
            issues'? <br>
            <br>
            Our quest to unfurl what goes on inside the mind of a CEO and what a 
            powerful corporate executive should stand for, took us to the 
            doorsteps of the leaders of a cross-section of business 
            organisations in Oman. The one overwhelming learning that we 
            acquired was that rapid changes in the business environment are 
            having a profound impact on the man at the top of the organisational 
            hierarchy, and making the jobs of such business barons extremely 
            excruciating. <br>
            <br>
            There is not only a huge change in the roles that a business leader 
            is expected to play today, it has also complicated the challenges 
            that CEOs face by making them more varied and diverse than ever. <br>
            <br>
            The CEOs have their hands full with the central strategic problems 
            of how to make their organisations grow beyond the scope of the 
            ordinary, taking advantage of the Internet and the global economy. 
            Secondly, they face certain everyday dilemmas of leading and 
            managing corporate Goliaths. And to top it, they have roles to play 
            on the local, regional and global socio-economic stage. </font></p>
