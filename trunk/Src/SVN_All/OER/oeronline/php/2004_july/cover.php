            <p align="justify"><font face="Verdana" size="1">Minoo Saher, group chief executive officer of Mustafa Sultan 
            Enterprises (MSE), takes the argument even further by suggesting 
            that countries like Oman have to run like corporations, very much on 
            the lines of Dubai and Singapore. &quot;Oman can learn from the 
            experience of senior management leaders on how to institutionalise 
            processes so that the inefficiency and wastage in the system is 
            weeded out or reduced, productivity is enhanced and, most 
            importantly, when you see yourself a party to building a society, 
            you will do much more than what you are currently doing. We should 
            look at ways to involve the country's business leaders in the 
            nation-building debate.'' On the other hand, Ajay M. Khimji, 
            partner, Khimji Ramdas (KR), would want business leaders to &quot;work 
            together typically in a family-oriented manner.'' <br>
            <br>
            We all know that Bill Gates, Ted Turner, Michael Dell, John 
            Chambers, Jeff Bezos, Steve Case and Meg Whitman participate in some 
            form of philanthropy - whether it is setting up foundations, 
            participating with groups, or merely signing a cheque from time to 
            time. However, a CEO who is active philanthropically may set an 
            example to employees. &quot;Businesspeople should draw attention to those 
            who are less well-off. There is enormous amount of good work being 
            done--charities, day-to-day philanthropy, donations etc.--which 
            never get seen because it isn't fashionable. But it is fashionable 
            to drive around in a great expensive car, is it? I am a bit nervous 
            about this constant attitude of win at any cost,'' cautions Hobbs.
            <br>
            <br>
            In the past few years, CEOs of some of the largest companies in the 
            US have taken on a celebrity status. Bill Gates, Larry Ellison, 
            Carly Fiorina and Jack Welch have become icons and role models in 
            much the same way as Tom Cruise, Tom Hanks and Catherine Zeta-Jones. 
            Their images are carefully crafted in the media and their public 
            statements are designed to project leadership and global values. 
            Perceptions do matter and senior management needs to embody and 
            expand a company's values and vision in a way that is compelling to 
            investors. In consequence, CEO reputation should be actively managed 
            as a corporate asset consistent with company philosophy. <br>
            <br>
            Times are tough for CEOs and successful executives must do more than 
            simply demonstrate integrity. Today's leaders need to serve as 
            positive role models for their employees - blending a strong moral 
            compass with the flexibility to adapt to new circumstances. In many 
            ways, the CEO is the guardian of corporate reputation. While the age 
            of the celebrity CEO may only recently be reaching the region, 
            clearly, the impact of the CEO on a company's reputation is still 
            very important. This has always been true in times of corporate 
            crises. <br>
            <br>
            <b>Changes CEOs cope with </b><br>
            In many ways, 2001 was a watershed year for company management 
            worldwide. The bursting of dotcom exuberance coupled with global 
            economic recession not only saw the collapse of untenable business 
            models, it brought into sharp relief far wider issues of business 
            accountability, financial valuations, corporate governance and the 
            level of trust that is given to CEOs. The rapid collapse of Enron, 
            formerly one of America's 'Most Admired' companies and the knock-on 
            impact to Arthur Andersen and the accounting industry are an 
            indication of what happens when that trust is lost. <br>
            <br>
            Locally too, repercussions of the global events left corporates 
            struggling, striving as they were to replicate the elusive growth 
            models of the 1990s. Events of 9/11 and the resulting crisis of 
            confidence that rippled across the globe combined with the economic 
            difficulties in the region to create widespread anxiety. The war in 
            Iraq and the ensuing political uncertainty have had a negative 
            impact on the investments in the region, in particular, and on 
            business in general. Dealing with all this and more, perhaps, is 
            part and parcel of being a business leader. <br>
            <br>
            As SOMC's Lawati puts it: &quot;I strongly believe that 'change = life', 
            and that 'if you stop changing, you stop living'. In general, 
            organisational success depends on effective responses to changes in 
            the environment and adaptation to those. However, a better position 
            to be in would be to 'intelligently' pre-empt changes and plan and 
            implement responses in advance; and yet, a further advantageous 
            position would be to 'create' changes that others then have to 
            follow.'' <br>
            <br>
            Agrees MSE's Saher: &quot;I believe the changes taking place outside are 
            extraordinarily rapid. One of the areas in which changes are 
            happening is in the way the customer is doing his business and, 
            therefore, the way he expects us to deliver. Customer expectation is 
            going through a dramatic change and I believe that I need to 
            constantly keep an eye on that. I need to be sensitive about the 
            trends outside - internationally, regionally as well as in the 
            country. As a CEO, I must be able to spot a trend early enough so 
            that we are not in a state of un-preparedness when it actually hits 
            us. I also keep on looking at what world-class companies are doing 
            in terms of their processes and dealing with each other to be able 
            to emulate some of their best practices and benchmark against the 
            best.'' <br>
            <br>
            &quot;Intense change has been a large part of the financial Institutions 
            environment around the world for some time now; so, I am quite used 
            to living in an environment of change. I prefer it in fact. It 
            challenges me and the team to think creatively, to be resourceful 
            and responsive to look for new solutions to old problems,'' 
            maintains OIB's Carlough. CEOs must assume the challenging tasks of 
            anticipating and preparing for critical events as well as crafting 
            strategies for their companies to prosper amidst different 
            scenarios. <br>
            <br>
            No one, however, could have anticipated the confluence of critical 
            events that dominated 2002--ongoing terrorist attacks on multiple 
            continents, the protracted world economic downturn, and an 
            unprecedented crisis in corporate governance--yet, it remains the 
            role of the CEO to prepare for, and to lead through, these and other 
            challenges. Carlough, for one, enjoys continuous change and, in 
            fact, suggests that he thrives in the process. &quot;I believe my success 
            in the past has been based on my ability to create change in the 
            workplace, not for the sake of change, but for continued 
            organisational improvement. So, for me, these times of extraordinary 
            change are a part of my normal work day. I wouldn't want it any 
            other way.'' <br>
            <br>
            MSE's Saher defines his role as someone who has to find ways and 
            means to enhance the performance of the organisation and ensure that 
            it succeeds. KR's Khimji, on the other hand, has a simple yet 
            effective philosophy about his role as a business leader. &quot;I take 
            each day as it comes. In businesses, there are lots of ups and 
            downs, but I try to handle things in a peaceful manner, especially 
            while dealing with problems.'' <br>
            <br>
            <img border="0" src="oamc.jpg" align="left" hspace="5" width="175" height="203">&quot;Internally, in the context of what I sense happening externally, my 
            prime job is to set a direction and strategy that fits into the 
            strengths and the core competencies of the organisation on the one 
            hand and the customer's expectations on the other - something that 
            can create a distinctive value proposition in the mind of a 
            customer. Supporting strategies would be to make sure that there is 
            an alignment between our people, processes and technologies. When 
            that alignment is perfect, productivity is really high. When that 
            alignment is not right, something or the other does not happen in 
            spite of the good intentions. Finally, I ensure that within the 
            organisation, we have a holistic system which works as a 
            well-grooved mechanism,'' believes Saher. <br>
            <br>
            &quot;It is not only the extent of change that a corporate leader has to 
            deal with, but also the pace at which newer situations pose 
            themselves as hurdles to pass. The key challenge for a corporate 
            leader is thus to comprehend the change, devise response and manage 
            the change process. Hence, my job involves a constant monitoring of 
            strategic changes in the environment - both macro and micro - to 
            ensure our positioning is appropriate, and to timely and 
            appropriately trigger required response,'' sums up Lawati. <br>
            <br>
            <b>Challenges CEOs face </b><br>
            The rules for operating a business in this 'Age of Uncertainty' have 
            changed and the pressures on CEOs to manage effectively have 
            intensified. Managing confidence, be it of investors, employees, 
            customers or other stakeholders, is key to the survival of both the 
            business and the chief executive. &quot;One of the biggest challenges in 
            the region is the prevailing uncertainty due to the politics of the 
            region. If we were just blessed with a bit of stability and lack of 
            terrorism, it would materially affect how our airport develops,'' 
            believes OAMC's Hobbs. <br>
            <br>
            The biggest challenge facing corporates in the country today, 
            according to MSE's Saher, is how to grow in a flat environment. 
            &quot;Every organisation, to remain profitable, has to grow. And growth 
            in normal times necessarily comes from growth in the environment in 
            which you are operating. But if the environment is not growing at a 
            substantial rate, then how do you grow? Do you just extend your 
            portfolio so that you are now doing things which you weren't earlier 
            doing, or do you look at new markets in which to operate?'' he asks.
            <br>
            <br>
            Agrees SOMC's Lawati: &quot;In the context of change in Oman at the macro 
            level, I would think the common challenges would be the longer-term 
            economic scene, sustainable growth and the need to cope with the 
            changes in the socio-economic environment. There would naturally be 
            scores of other aspects of challenge, which would vary from one 
            industry to another and one entity to another.'' So also believes 
            OIB's Carlough. &quot;There are a number of interesting challenges 
            today,'' he claims. <br>
            <br>
            &quot;The team's first challenge is the limited opportunities in the 
            market. The market is also competitive, so my team has to be 
            creative, quick to respond and willing to go the extra mile. 
            Customers are certainly getting more price-sensitive, therefore, 
            there are new challenges to face and creative solutions to be 
            determined for asset and liability management in order to be able to 
            price product and services competitively in an effort to lead the 
            market,'' he enlists. <br>
            <br>
            Price-sensitivity is something about which MSE's Saher is concerned 
            too. &quot;When there are too many players in a market that isn't growing 
            very fast, there is an obvious pressure on price. So, maintaining 
            your gross margins and providing customers a value proposition so as 
            to maintain your bottomline is another challenge.'' KR's Khimji is 
            in agreement with the thought, proclaiming that &quot;growth and having 
            the best people in the right job are two of the biggest challenges 
            facing the country today.'' <br>
            <br>
            OIB's Carlough also believes that HR management has become a much 
            larger challenge in recent times. &quot;Middle managers are more mobile 
            today, shifting to a new organisation if they are not being 
            challenged, rewarded and compensated properly. This is compounded by 
            the lack of replacement resources available in the marketplace,'' he 
            says. As most products and services can be replicated today, the key 
            business differentiator is human capital. Agrees Saher: &quot;Attracting 
            the right people and giving them the career they're looking for in 
            order to reward and retain them to gain an edge over competition is 
            a challenge.'' <br>
            <br>
            That, as Saher himself acknowledges, is easier said than done. 
            &quot;Short-term profits and returns on investments many at times become 
            the end, and people are confused on whether to follow the process in 
            terms of customer satisfaction and handling of customers or should 
            they treat them as luxuries and hence ignore them in favour of 
            getting the job done and getting the numbers. It's a challenge 
            between the short and the long term. As an organisation, we have to 
            keep on building in a manner where the long term is as, if not more, 
            secure as the short term. For that, you have to keep on doing the 
            right things in terms of strategy and processes,'' he advocates. <br>
            <br>
            Lawati suggests working with the changes and the opportunities they 
            throw up rather than against them to overcome the challenges they 
            pose. &quot;The government has been playing a very important role in 
            nurturing the macro aspects of the socio-economic texture in the 
            country for the past three-and-a-half decades. The recent advent of 
            'privatisation' is now opening the floor for the private sector to 
            take a direct role, side by side with the government. This will 
            require a very high sense of responsibility on part of the corporate 
            sector, and it would be a mistake to expect easy and fast solutions. 
            Close work with the authorities will help the development of the apt 
            trade and socio-economic policies that will enable us to overcome 
            the challenges,'' he maintains. <br>
            <br>
            <b>Roles CEOs Play </b><br>
            Today's businesses and corporate environment are not limited to 
            business complexes and industrial estates. Corporate leaders in this 
            age and time have a much larger responsibility than plain and simple 
            profit-making, which extends to not only being a good corporate 
            citizen, but also participating in the socio-economic development of 
            the country. With that as an objective, clearly, corporate leaders 
            have to wear many hats in the present day scenario and the roles 
            that they play are myriad and complex. A CEO has to be directive and 
            participative, teacher and student, challenging and accepting, 
            leader and follower - all at the same time. Put simply, there's no 
            one-size-fits-all solution for the CEO - no single leadership style 
            works on all occasions. CEOs need to experiment with a wide variety 
            of styles and be open to learn from experience. <br>
            <br>
            &quot;I think of a CEO's role very much in terms of a conductor of an 
            orchestra. His job is to ensure that the total piece of music that 
            the audience hears is as good as they expected to hear. Now that 
            starts off right from deciding what piece of music should be played 
            - i.e., in OAMC's case, what shape do we want the airport to look 
            like in 5 years' time, to the strategy of getting new business, how 
            we are placed in terms of safety of operations, and what systems and 
            equipment do we need in order for us to get to our primary two 
            objectives - firstly, a safe and secure operations, and secondly, 
            something that will delight the customer,'' suggests Hobbs. <br>
            <br>
            &quot;Then, we assemble the team and we let them know what exactly is 
            expected of them - what piece of music we want played. We don't send 
            them away to do their own thing - to learn their violin part or the 
            trumpet part or whatever. And we monitor what they are doing by 
            visiting each area. Then we bring them all together - we have to 
            bring them all together 24 hours a day, seven days a week, whereas a 
            piece of music is played just once,'' he adds. <br>
            <br>
            That, in a nutshell, is how a business leader is expected to perform 
            - not only to keep the complete picture in mind at all times, but 
            also ensure that the executives--frontline or those at the backend 
            of operations--know how important their pieces are in the overall 
            completion of that picture. &quot;I generally see myself as wanting to 
            look at the complete picture, the complications within it, then 
            ultimately try and simplify it - first in my mind in terms of how I 
            read those signals, and then communicate it to the company in a 
            simple manner. We are an organisation of 400 people, and different 
            trends, signals mean different thing to different people. If you 
            create a very complicated verbal scenario, then everyone will not 
            understand what we are saying. So, to bring it down to a single 
            simple shared business objective and philosophy is how I see my 
            job,'' explains Saher. <br>
            <br>
            &quot;In an airport, we have a vast number of customers who actually can 
            have more impact on the passenger than we do. If the check-in queues 
            are long, it is the individual airlines who check passengers. It's 
            us that have to try and motivate all those other customers so that 
            the whole team gives the passenger a seamless service. That is where 
            my skills of persuasion, discussion and negotiation come handy in 
            making them understand that we're all going to get more business if 
            we provide a safer and a more professional operation. Now there 
            always are disputes and there are successes. My job is to accentuate 
            the successes and resolve the disputes as easily as possible. Like 
            that piece of orchestra, if one of the first violins is playing a 
            bum note, then you go and help him,'' describes Hobbs. <br>
            <br>
            As Lawati puts it: &quot;I look at myself as two 'personalities' (at 
            least): one, in the corporate executive position in question, and 
            two, the human being with own, family and other social commitments 
            and roles. Each of these two roles has different requirements, 
            probably changing also in different ways, and I might differ with 
            those wishing to look at both in an integrated fashion. Such an 
            approach would stipulate that changing roles in the corporate life 
            of a person should get reflected on his private life - and I 
            certainly do not subscribe to this school. Hence, this kicks the 
            matter off on a rather complex platform of 'duality'.'' </font></p>
