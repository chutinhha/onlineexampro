                              2004. The CEOs were asked to step back from their 
                              daily challenges and to provide their thoughts, 
                              their hopes and fears, and their insights into how 
                              to lead through these uncertain times. They were 
                              asked to share their strategies and tactics for 
                              coping with the concerns about the political 
                              uncertainties, with challenging capital markets, 
                              and with the erosion of the public trust. Looking 
                              ahead, what are the prospects for real growth in 
                              the post-Internet bubble, post-Enron economy? What 
                              are the tools and strategies CEOs can use to 
                              create and sustain real value? What can CEOs do to 
