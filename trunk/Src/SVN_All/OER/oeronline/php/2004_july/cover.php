            <b>Accountability CEOs have</b> <br>
            Even a decade ago, it was an easier time to be CEO or chairman. The 
            management model was largely based on command and control. Top 
            management was largely free from accountability, save to 
            shareholders and sometimes governments, and the chief executive's 
            fame was limited to his peers, his clubs and to shareholders. <br>
            <br>
            This traditional management model, while long dead in the US and 
            becoming extinct in Europe, is still alive in the Middle East and 
            many parts of Asia. Change is coming, however, in part through 
            internal generational change with new leaders assuming control of 
            family-controlled businesses and introducing new management 
            techniques or going to the markets in search of capital, and in part 
            through external changes brought about by new corporate governance 
            regulations and the demand from shareholders for greater 
            accountability. <br>
            <br>
            Another challenge that business houses face today includes setting a 
            high level of a moral and ethical code within the whole system. 
            &quot;Corporate governance implementation, monitoring of compliance, 
            improving the general understanding of the concepts and building 
            acceptance amongst the management team across corporate Oman has to 
            be one of the biggest challenges facing the country today,'' 
            maintains Carlough. &quot;Countries like Singapore have shown to the 
            world that you can be committed to certain set of core values in the 
            nation-building activity. Business leaders and houses have a 
            difficult role to play to set that norm,'' propounds Saher. <br>
            <br>
            &quot;There has to be a mechanism in place through which commitment to 
            certain value systems has to be ingrained. Wipro and Infosys of 
            India are two companies that have reached a turnover of a billion 
            dollars and are at the same time committed to a certain value system 
            - here's proof that it's a myth to believe that if you are committed 
            to a value system, then progress is retarded,'' he adds. In fact, 
            Saher believes that if such a business environment progresses in 
            Oman, we will be able to attract the right kind of multinationals 
            and thus will be able to attract foreign investment and the right 
            kind of talent, and the system will flourish over a period of 
            time.'' </font></td>
