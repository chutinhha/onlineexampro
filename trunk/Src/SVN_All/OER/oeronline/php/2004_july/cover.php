            <b>Power CEOs wield </b><br>
            <img border="0" src="cover4.jpg" align="left" width="130" height="250">Given 
            the changing nature of business, CEOs need to devote time and energy 
            to social, political and economic causes since they can influence 
            society and spearhead changes. Are CEOs in Oman actually doing that? 
            Fundamentally, how do corporate head-honchos in Oman construe the 
            job of a business leader in this time of extraordinary and rapid 
            change? &quot;Businesses both influence the society as well as are 
            influenced by the society. It is important that the business leader 
            understands these two aspects and the extent to which his/her 
            individual corporate entity relates to the society,'' answers Irshad 
            Lawati, managing director, Shell Oman Marketing Company (SOMC), the 
            Sultanate's largest public listed company. <br>
            <br>
            &quot;In Shell Oman, we strongly believe in both the aspects and pay a 
            lot of attention to the impact of our activities on the society as 
            well as the role that we as corporate citizens should play in the 
            society. We approach it under the broad context of our 'Sustainable 
            Development' strategy which postulates a proactive role to be played 
            in managing the relationship in a way that leads to optimisation of 
            positive impact on our surroundings,'' he claims. <br>
            <br>
            &quot;Business leaders should set an example. The higher the profile you 
            have, the higher the responsibility you have to reflect that back on 
            society,'' agrees Colin Hobbs, chief executive, Oman Airports 
            Management Company (OAMC). <br>
            <br>
            &quot;I think anybody who is high profiled--be it a pop star or a 
            business leader or a politician--has an enormous responsibility to 
            set a good example for those around,'' he adds. &quot;Business leaders 
            should LEAD; lead by example and do so decisively and intensely. The 
            world is full of decent, good, qualified managers. There is, 
            however, in my opinion a shortfall of strong, focused, resourceful 
            leadership,'' maintains John Carlough, chief executive officer of 
            Oman International Bank (OIB). </font></td>
