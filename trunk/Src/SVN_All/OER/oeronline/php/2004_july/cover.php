            a CEO</font><b><font face="Verdana" size="1"><br>
            <br>
            What role do our most important business leaders play today?&nbsp; 
            How do the CEOs of major corporations construe their&nbsp; jobs in 
            this time of extraordinary and rapid change? Vicky Kapur maps the 
            mind of&nbsp; a CEO and finds out&nbsp;&nbsp;&nbsp; what the 
            country's head-honchos do to rejuvenate their mind and soul when 
            they are over and done with boardroom boredom, jetlag jitters and 
            stock market shocks</font></b></p>
