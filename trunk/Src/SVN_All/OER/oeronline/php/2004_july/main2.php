<p align="justify"><font face="Verdana" size="4" color="#FF0000">The adaptive 
enterprise<br>
</font><font face="Verdana" size="1"><b><br>
Konstantinos G. Apostolatos presents a new corporate model in times of permanent 
volatility</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="adaptive.jpg" align="left" hspace="5" width="200" height="198"></font><font size="4" face="Times New Roman">O</font><font face="Verdana" size="1">ver 
the past few years, we have seen almost unprecedented volatility in the global 
economy, leading to the demise of a large number of renowned corporations: 
Enron, Global Crossing, Kmart, Swissair, One.Tel, Excite at Home, L&amp;H, Kia 
Motors, Polaroid, Webvan, Pacific Gas &amp; Electric Co., Andersen, Daewoo, 
Comdisco... the list goes on and on. Who could have imagined all this? Many of 
our corporate 'heroes'/CEOs have gone down into infamy. What is happening? <br>
<br>
Predictably, traditional corporations have tried to respond to this recession in 
the usual ways - by introducing large cost-cutting programmes, freezing 
recruitment and research, putting all new investments on hold and waiting until 
the storm passes... However, there is compelling empirical evidence that 
business change is becoming more frequent, more sudden, and less predictable. 
The changes are evident in all aspects of the economy - resulting in dramatic 
increases in volatility, more business failures, and less predictable earnings. 
This form of volatility is not going away. It is now a permanent part of our 
economic system. <br>
<br>
As Sir John Browne, former CEO of BP Amoco, said: ÒGiving up the illusion that 
you can predict the future is a very liberating moment. All you can do is to 
give yourself the capacity to respond... The creation of this capacity is the 
purpose of strategy.&quot; <br>
<br>
What has caused this dramatic change? We believe it is the growing pervasiveness 
and power of connectivity and technology in our increasingly global economy, a 
dramatic reduction in product life cycles, and the blurring of traditional 
definitions of markets and competition. <br>
<br>
Nature has dealt with permanent volatility for billions of years, and the most 
successful species have learned to adapt to significant changes in their 
environment. Their dynamic change has allowed them to compete with other 
species, even in unstable environments. It would appear that we are entering 
into the era of the Adaptive Enterprise. <br>
<br>
We define an 'adaptive' organisation as one that is able to permanently 
recombine and transform to benefit from business volatility. Adaptive 
organisations demonstrate the following features: <br>
<br>
<b>Sense and respond: </b>The ability to permanently scan and anticipate the 
marketplace and provide 'modular system and service solutions' (MSSS - you 
notice that we are not talking about 'products' in the traditional sense) before 
the competition. This can be achieved through continuous testing, anticipating 
and influencing demand, and 'shared and lived' customer knowledge. Companies 
such as Capital One, Dell, Cisco have successfully been implementing such 
concepts. <br>
<br>
<b>Plug and play:</b> The ability to continuously combine technology modules/ 
components and internal/external (network) capabilities into new offerings. Sun 
Microsystems has used such concepts in product development by the use of modular 
and open standards, whereas Zara (contemporary fashion stores) has used variable 
network scalability concepts to variabilise its product/service delivery 
capability. <br>
<br>
<b>Learn and leverage:</b> This refers to the ability to build permanent 
knowledge/ feedback loops through knowledge sharing, capitalising on customer 
experience, and turning every mistake into a winning solution. Companies such as 
BP, Microsoft, L'Oreal have championed such approaches with a large degree of 
success. <br>
<br>
The enabling drivers of such enterprises are to be found in new technologies, 
new people capabilities, and emerging governance models. Technology is the 
enabling driver that has evolved the most rapidly: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Ubiquitously connected devices using rapidly emerging industry standards enable 
companies to share, read and use (relevant only) data on demand. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Specific and sensitive systems with tremendous processing power read and react 
to precision data on events real time, allowing dynamic management of business 
events. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Modular, standard-based Web and mobile architecture allows companies to combine 
and re-combine capabilities on a component basis, vastly reducing technology 
switching costs, and increasing agility. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Complex techniques such as game theory (Ford, Nucor), system dynamics (Intel, 
Syngenta), agent-based modelling (CEMEX, Unilever), real options (HP, Acer) can 
now be used extensively. <br>
<br>
Consider a couple of examples. Large FMCGs such as P&amp;G are experimenting with 
Web-based/genetic algorithm-based 'intelligent agents' to 'genetically profile' 
consumer preferences. Daimler-Chrysler has piloted a multi-agent system at its 
Stuttgart factory, which allows real-time, fully automated work order processing 
and machine scheduling. Governance models will also have to adapt.<br>
<br>
'Back to core competences' is relevant once again. More than ten years ago, 
James Brian Quinn in his Learning Organisation stressed that virtually all 
companies are becoming service companies and that they should only perform those 
activities in which they have the chance to be the 'best in the world'. 
Everything else should be outsourced, or otherwise economic value is being 
destroyed. A company such as Nike championed such an approach many years ago by 
retaining only marketing and R&amp;D as key functions, and outsourcing all the rest.
<br>
<br>
Assuming that companies organise around top capabilities which can be recombined 
and re-shuffled as needed to exploit 'horizontal markets', as opposed to 
structures or even processes, this poses a serious question to future governance 
models. <br>
<br>
We are no longer talking about managing a traditionally organised business 
entity but rather managing 'capability modules' within an eco-system (the 
Japanese pioneered working within an eco-system a few decades ago with their 
Keiretsu concept, except that this was a tightly regulated, local, dependent 
network of partners with a strong leader and protectionist boundaries) of 
partners, customers, suppliers. <br>
<br>
There may also be some similarities to venture capital firms managing a 
portfolio of businesses but there will be significant enhancements, adding 
intelligence to the model, as VCs do not manage capabilities but pure financial 
assets. At any rate, emerging governance models will have to: <br>
<br>
<b>Enable self-organisation:</b> Specify rules/ terms, not a detailed plan, and 
empower employees with more connections, information, and freedom. Encourage 
experimentation to speed innovation, and regard failures as an investment. <br>
<br>
<b>Recombine to reinvent:</b> Borrow ideas and practices freely, make offerings 
upgradeable, breed ideas and processes early and often, and view interchangeable 
modules for people and products as essential for mass customisation. <br>
<br>
<b>Make boundaries permeable:</b> Increase the number and density of 
connections/dynamic partnerships with the environment to speed information flow 
and adaptation, be transparent with information, and promote diversity of 
opinions and experience to speed innovation. <br>
<br>
<b>Close feedback loops:</b> Measure the company and market reactions 
continuously and in real time, understand and measure intangible assets, and 
avoid anonymity to increase trust, reputation, and information flow. <br>
<br>
<b>Apply selective pressure: </b>Balance encouraging diversity with filtering 
the non-performing part of ventures, people, processes, systems, values - 
everything. Manage the network to remove less productive partners, and demand 
continuous innovation.<br>
<br>
<b>Live at the edge of chaos:</b> Sense changes in the environment and respond, 
embracing disequilibrium over stability, and use more resources to explore new 
possibilities for your business. <br>
<br>
Finally, there is a move towards more adaptive people and culture profiles: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> More 
people are prepared to work based on increased entrepreneurship, autonomy, 
accountability. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
concept of the employee is becoming outdated - people will operate within an 
enterprise's eco-system instead of within a specific unit. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> More 
people are seeking new challenges based on their capabilities and intelligence, 
fun and fulfilment, and work/life balance. People will be given the space to 
redefine and even destroy and re-invent their business. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
focus will be lifetime fulfilment, instead of lifetime employment. <br>
<br>
What is the value to a company of becoming adaptive? In this environment of 
rapid economic change, it has been found that more adaptive organisations 
perform significantly better than their non-adaptive counterparts. Research from 
CGE&amp;Y's Centre of Business Innovation revealed that over the last ten years, 
adaptive companies have had a significantly higher average annual growth in 
earnings than non-adaptive organisations. In addition, the average return on 
equity for adaptive organisations is twice that of non-adaptive companies. <br>
<br>
As organisations become more connected with their environment, they become more 
like living organisms and are governed by the same rules of adaptation and 
evolution. No organisation can ignore the reality of permanent economic 
volatility or the success of nature in overcoming constant change. Now is the 
time to develop an adaptive approach to business and enable your organisation to 
thrive in this new world. It should however be emphasised that we are in the 
early stages of this corporate evolution and that few companies have started 
moving down this path successfully. </font></p>

