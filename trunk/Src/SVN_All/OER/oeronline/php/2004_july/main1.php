            <font face="Verdana" size="4" color="#FF0000">Brandishing brand 
            names</font><p align="justify"><font face="Verdana" size="1">
            <b>Building and maintaining strong brands using traditional tools is 
            getting extremely tougher and costlier. Shuchita Kapur analyses how 
            local marketers are cutting through the clutter to better promote 
            the brands they handle while carefully nurturing their own image to 
            capitalise on the post-WTO windfall</b><br>
            <br>
            </font><font size="4" face="Times New Roman">S</font><font face="Verdana" size="1">hrinking 
            marketing budgets and an explosion in the number of brands are 
            making it increasingly difficult to build strong brands. Top it up 
            with a proliferation of ways to communicate a brand to the 
            customer--from cable channels to the Internet, product placement in 
            movies, and even mobile-phone display screens--and the job of a 
            marketer doesn't seem enviable anymore. Rising above the clutter 
            without breaking the bank requires companies to get smarter about 
            branding. <br>
            <br>
            To cut through chaos, a company must go beyond intuition and turn to 
            a more rigorous, fact-based analysis of the effectiveness of its 
            brands. To deliver them, Marketing must develop new skills and 
            obtain help from other departments, including product development, 
            operations, and customer service. <br>
            <br>
            <b>
            <img border="0" src="kr1.jpg" align="left" hspace="6" width="157" height="174">Better branding </b><br>
            During the 1990s, marketers spent unprecedented sums of money, but 
            many discovered later that more wasn't better. They relied too 
            heavily on intuition about the way their products should be branded. 
            They thought just by flushing too much money into branding 
            themselves and their products, they could guide customers to their 
            doorsteps. Their way of branding was not based on a fact-based 
            understanding of the marketplace. Times have changed, and so has the 
            concept of branding. In today's world, companies have become smarter 
            about branding without stretching their budgets too much. These 
            days, smart companies all around the world focus on aspects of the 
            brand that influence customer buying patterns, and Oman is no 
            exception to the rule. Most companies in the Sultanate have started 
            building their brands more scientifically. <br>
            <br>
            As a spokesperson of the Saud Bahwan Group (SBG) puts it: &quot;There has 
            been a gradual yet discernible change in the concept of branding in 
            Oman. There was a point in time when it was felt that price had 
            become the sole determinant for purchase. However, as market 
            matured, customers became more sophisticated in their approach. 
            Brand values have become very important in present time. Today, 
            building a distinct brand identity has become a top marketing 
            priority for all thinking marketers.'' His view is reiterated by 
            Viveck C. Pande, GM, lifestyle group of Khimji Ramdas (KR): &quot;These 
            days, it (branding) is basically capturing the mind of the 
            customers. It all starts from consumer insight.''<br>
            <br>
            <b>Customer segmentation <br>
            </b>The first order of business is to take a hard look at the 
            long-term profit potential of each customer segment. Otherwise, 
            marketers can waste a huge amount of effort defining and delivering 
            brands for segments that don't warrant the investment. This can be 
            done by studying everything from loyalty programmes to 
            Internet-based surveys. Most companies maintain a database of 
            customers so that it becomes easy to target the right segment.<br>
            <br>
            On the other hand, there are companies that have a broad bouquet of 
            products and target literally everybody. For instance, the SBG 
            manages a huge portfolio of brands, representing a widely 
            diversified set of business activities. &quot;Given the width of our 
            business activities, the target audience for our brands covers 
            almost every segment of Omani society and encompasses all kinds of 
            customer profiles,&quot; says an SBG spokesperson. <br>
            <br>
            <img border="0" src="muscat.jpg" align="right" hspace="6" width="154" height="180">Shawn Varghese, marketing manager of Muscat Electronics, too feels 
            that his company caters to the entire population in Oman through the 
            six brands it markets. According to him, &quot;The company caters to 
            anybody and everybody, be it an Omani, an expatriate, young, old, 
            men and women. We have a Play Station which attracts a child and a 
            spit air-conditioner which attracts a corporate person, a microwave 
            which attracts a housewife and a plasma TV which attracts people 
            from high society. So, it is basically a varied lot.&quot; <br>
            <br>
            Same holds true for the Suhail Bahwan group. &quot;As we market a wide 
            range of products and services, the entire population of the 
            Sultanate is our target audience. We have a brand/ product for every 
            consumer in town. Product categories include automobiles, consumer 
            durables, FMCG, building material, healthcare products and 
            industrial products. The services we offer include engineering 
            construction, telecommunications, travel &amp; tours, information 
            technology, shipping &amp; transport and specialised maintenance 
            services,'' argues Ketan Mehta, advertising manager at the Suhail 
            Bahwan group. <br>
            <br>
            The same broad segmentation, albeit minus the kids segment, goes for 
            AutoPlus, a company that claims to cater to every adult's needs. 
            AutoPlus has small-, medium- and big-sized cars. They offer a family 
            car, a sedan, a coupe, or a sports vehicle. Besides, they also have 
            a range of second-hand cars in their showroom. Basically, it is a 
            place where you can find all kinds of cars under one roof. Then 
            there are the likes of Khimji Megastore, which have undertook 
            detailed studies of their customers and their buying patterns, and 
            have arrived at a conclusion that their consumers include a majority 
            of local nationals between the age-group of 25 and 45. <br>
            <br>
            <b>Trend spotting </b><br>
            Once the customers have been segmented and the potential customers 
            are known, a company should understand the needs of their clients. 
            Understanding the buying patterns, likes and dislikes of customers, 
            can auger well for a marketer. They should not only be identified 
            but also embraced. Shawn Varghese at Muscat Electronics feels that, 
            in Oman, the driving force in marketing strategies is 'sale'. &quot;If 
            you have to market a product here, you have to mention the word 
            'SALE!'. After extensive research, we have come to the conclusion 
            that you have to have the word sale to attract the customers. Raffle 
            draw and things like scratch-and-win are not very lucrative to 
            attract customers. That is what appeals to most people in Oman. The 
            instant benefit that accrues on a product is of utmost importance&quot;, 
            he says. <br>
            <br>
            Besides sale, Varghese feels after-sale service is the next 
            important factor in attracting customers. &quot;Despite having a lot of 
            Indian customers, our database showed that there were many Indians 
            who were not buying Sony products. On doing a market research, we 
            found out that the reason for that was that products bought in Oman 
            did not enjoy warranty in India. So, we got in touch with our Indian 
            partners and asked them to offer warranty on products sold here in 
            Oman. This brought those Indians in our fold who were not buying 
            from us only because of the warranty issue,&quot; he adds. <br>
            <br>
            Mohamed A. Al Kindi, deputy general manager of AutoPlus, feels 
            understanding customer needs is of utmost importance. &quot;We carried 
            out extensive studies to find out whether it was the price or the 
            service that drove customer buying patterns. We concluded that while 
            reaching out to a customer base was not difficult, retaining one can 
            be very difficult. We feel the mantra is to not just satisfy our 
            customers' existing needs, but being ahead of customer 
            expectations.'' <br>
            <br>
            <b>Building the brand </b><br>
            Once marketers have segmented their customer base and spotted 
            meaningful trends, the next challenge for a company is to make its 
            presence known by effective advertising--an increasingly complex 
            process. Brand proliferation and rapid imitation have diminished the 
            return on clever advertising and 'breakthrough ideas,' such as 
            adding a 'miracle ingredient' to a detergent or associating a sports 
            star with a particular brand of athletic shoes. <br>
            <br>
            Today, cost-effective brand building depends on knowing precisely 
            what consumers care about and tailoring the brand accordingly. 
            Sophisticated new analytic approaches provide the precision but only 
            when coupled with conceptual clarity in first defining a brand and 
            then actually delivering it through a variety of what marketers call 
            'touchpoints,' the sites where consumers interact with it. <br>
            <br>
            The tools that a marketer employs for marketing and brand building 
            are extremely important. The golden rule, according to Varghese, is 
            visibility and display. &quot;Even if you go to Ruwi High Street 
            showrooms, you will see a very strong presence of the brands that we 
            have vis-à-vis our competitors, and this gives the customers the 
            message that these products are the fastest moving and the 
            established ones in the market,'' he claims. <br>
            <br>
            The call of the day is for a more personal advertising than the 
            usual adverts in the print media. Tech smart Khimji Ramdas believes 
            in sending SMS' to advertise their various pizza's at the Pizza Hut. 
            &quot;The pervasiveness of text messages is high and very cost 
            effective,'' says KR's Pande. They also design their advertisements 
            according to the needs of the customers. <br>
            <br>
            For example, the adverts of their traditional pizza emphasise the 
            message of value for money. Targeted at the home segment, the advert 
            talks about this bigger 15&quot; pizza than the normal 13&quot; pizza, which 
            comes at a good price too. Also, their Thursday Thunder programme 
            which goes live on air from their Pizza Hut outlet at Qurum is a 
            good way of advertising. They not only advertise their brand in an 
            effective manner, but also gain popularity as they get numerous 
            calls from teenagers requesting to be featured on their programme.
            <br>
            <br>
            The SBG cashes in on the entire range of above-the-line and 
            below-the-line media available. For a mass brand like the Corolla, 
            its focus is on the mass media such as press, radio, TV, road shows 
            and events. However, for a premium brand like the Avalon, the focus 
            is on the direct mailers and other forms of one-on-one 
            communication. <br>
            <br>
            TV commercials are another good way of advertising but can be very 
            expensive. However, if resources are pooled, it can become an 
            effective way to push your brand in thousands of homes at the same 
            time. KR's Pande emphasises the importance of such a strategy. &quot;Our 
            distributors in the Middle East have formed a cooperative and have 
            pooled in their resources for broadcasting TV commercials. It is a 
            very expensive exercise, and I'm certain that none of us could have 
            managed it alone. But you can well imagine its effectiveness. Our 
            brand message penetrates so many homes across the GCC,'' he affirms.
            <br>
            <br>
            Mehta of the Suhail Bahwan Group is a fan of below-the-line 
            activities. &quot;While we do communicate the brand messages through 
            traditional above-the-line media, we put more emphasis on 
            below-the-line activities which have a far better reach in the 
            interiors of the Sultanate. Because communication vehicles are still 
            not fully developed in Oman and a lot of people do not buy local 
            dailies or periodicals, we have to keep innovating new methods of 
            communications. We regularly conduct brand image surveys. Market 
            research plays an important part in formulating our communication 
            strategy. Such research gives us insights into the likes, dislikes 
            and preferences of the consumer vis-à-vis our brands and helps us 
            serve them better,'' he opines. <br>
            <br>
            Another good way of advertising may be by combining social service 
            with business. AutoPlus was smart enough to participate in The 
            Spirit Race in April. It not only highlighted its products, but also 
            complimented its presence by focusing on issues such as safe 
            driving. In fact, a lot of smart marketers are doing that to 
            maintain top-of-mind recall. Shell Oman Marketing, for one, has 
            partnered with the Ministry of Commerce &amp; Industry and launched the 
            'My Beautiful Oman' campaign (see OER, June 2004, issue for details) 
            to develop domestic tourism in the country. While initiatives such 
            as participating in a race or partnering to develop tourism are not 
            going to immediately end up in more customers buying more of your 
            products or services, they do go a long way in building a brand and 
            associating the right attributes to it in the consumers' minds. <br>
            <br>
            <b>Serving the customers </b><br>
            <img border="0" src="toyota.jpg" align="left" hspace="6" width="150" height="134">Certainly, good adverts can bring the customers to a showroom, but 
            the most important task is to hold them back so that they buy the 
            products and, in the long term, become loyal customers. This is 
            where the importance of sales and customer service division comes 
            into play. They are no less important than the marketing teams on 
            the road. An unattended customer is as good as a lost one. <br>
            <br>
            According to the SBG spokesperson, &quot;one major factor that has made 
            us market leaders in almost every category is our customer 
            orientation, our ability to listen and keep a track of the changing 
            customer needs and our ability to build product and service 
            attributes that satisfy the constantly changing customer needs. A 
            one-line summary of our corporate goal is 'customer satisfaction No. 
            1'.&quot; <br>
            <br>
            Personal attention, according to Muscat Electronics' Varghese, spins 
            the ball. If you go to large retail outlets, he feels you can never 
            get personal attention. But, at Muscat Electronics, we attend to 
            each individual's needs. We not only display our products, but also 
            recommend the best one they can have.&quot; he adds. <br>
            <br>
            <img border="0" src="kindi.jpg" align="right" hspace="6" width="155" height="179">AutoPlus' Al Kindi feels the same. &quot;People can get the same product 
            from different places - so, it is basically the service that becomes 
            more important. At AutoPlus, we not only show the customers the car 
            they want, but also offer them the best that they can get keeping in 
            mind their budget. Moreover, we offer them instalments, avoiding 
            them the hassle of an outside agency.'' <br>
            <br>
            So believes Mehta, &quot;We consider understanding and anticipating 
            customer needs as crucial to our continued success. We believe in 
            'customers for life' concept and strive to give the greatest value 
            for money to our customers by providing world-class products and 
            services. <br>
            <br>
            Simply serving the consumer gives longevity to a brand. By just 
            offering a simple glass of water or a can of cold drink, the 
            customer can feel welcomed. In return, the image that a brand gets 
            is one that which serves the customers. <br>
            <br>
            <b>What's my brand, anyway? </b><br>
            Defining a brand involves emphasising its key benefits and 
            attributes for consumers. To do so, marketers must recognise that a 
            brand consists of more than a bundle of tangible, functional 
            attributes; its intangible, emotional benefits, along with its 
            'identity', frequently serve as the basis for long-term competitive 
            differentiation and sustained loyalty. Coca-Cola, for instance, is a 
            powerful global brand not just because the beverage comes in a 
            familiar red can and customers like the taste, but also because it 
            conveys an image as an optimistic product. <br>
            <br>
            Explains Mehta: &quot;There is an interesting research being done to 
            understand consumer preferences. The finding is startling - brain 
            scans reveal powerful proof that we may prefer Pepsi, but we'll buy 
            Coke. Read Montague, the director of the Human Neuro-imaging Lab at 
            Baylor College of Medicine, has now provided a scientific proof that 
            branding plays with our brains. <br>
            <br>
            &quot;Using a non-invasive technique called functional magnetic resonance 
            imaging (fMRI), Thought Sciences marketing analysts use information 
            to more accurately measure consumer preference, and then apply this 
            knowledge to help marketers better create products and services and 
            to design more effective marketing campaigns. <br>
            <br>
            &quot;When Montague gave a taste of an unnamed soda to his volunteers, he 
            found that more people preferred Pepsi. The shock came when he 
            repeated the experiment, this time telling volunteers which brand 
            they were tasting. Nearly all the subjects said Coke. <br>
            <br>
            &quot;Most people prefer the taste of Pepsi, and yet, globally, Coke 
            sells more. Why? Branding! Is it something simple and stupid like 
            the red can, the curvy script or the hard consonants, or something 
            much more complex? The idea that neuroscience has applications for 
            business is causing a wave of excitement in the marketing sector. I 
            hope it makes our--marketing professionals'--life easier to 
            understand consumer preferences.''<br>
            <br>
            Marketers could promote many tangible and intangible brand 
            attributes, but the goal is to uncover the relevance of each to 
            consumers and the degree to which it helps distinguish the brand 
            from those of competitors. The number of brand elements at play and 
            the interdependencies that often exist between tangible and 
            intangible attributes can make these assessments complex. 
            Fortunately, statistical techniques can now increase their 
            reliability. As such an analysis often shows, certain features may 
            differentiate a brand from its competitors but don't matter to 
            customers. These attributes are the fool's gold of branding. <br>
            <br>
            Some attributes, however, are important even though customers expect 
            them from any competitor. The most successful brands emphasise 
            features that are both important to consumers and quite 
            differentiated from those of competitors (see chart). These are the 
            'brand drivers.' <br>
            <br>
            <b>Getting rid of guesswork </b><br>
            Recognising antes, drivers, and touchpoints is difficult enough in 
            retrospect. How should companies choose the right ones 
            prospectively? Traditionally, the elements that deliver a brand's 
            value to the consumer have been identified through costly trial and 
            error. The process involves posing direct questions about a brand's 
            functional benefits, analysing the results through techniques such 
            as conjoint analysis, and then taking a series of creative leaps 
            that qualitative research may not validate. <br>
            <br>
            Although this approach has been useful over the years, its 
            functional focus runs the risk of overlooking a brand's subtler, 
            intangible dimensions. Traditional techniques are also ill-equipped 
            to identify with precision the relationship between a brand's 
            attributes and the most cost-effective touchpoints for delivering 
            them. Finally, the experimentation that is part and parcel of 
            traditional techniques can have the unintended consequence of 
            confusing consumers by emphasizing first one set of brand 
            attributes, then another.<br>
            <br>
            Today, marketers can eliminate much of the guesswork by applying 
            social-science techniques to identify the underlying brand 
            attributes driving loyalty among specific customers. Known as 
            pathway (or structural-equation) modelling, these techniques aren't 
            new; they rely heavily on fundamental regression techniques. But 
            they are only now beginning to be applied to branding as marketers 
            become more and more aware that targeting precisely what customers 
            care about is the core of efficient brand building.<br>
            <br>
            Developing this understanding is possible today because of the 
            information boom generated by diverse factors (such as loyalty 
            programs, cheaper Internet-based consumer surveys, and electronic 
            point-of-sale data) and by attitude-based research from third-party 
            research firms as well as the availability of increasingly 
            sophisticated and accessible data-analysis packages. <br>
            <br>
            <b>Corporate branding </b><br>
            In the Omani market, building a corporate brand is as important as 
            building those of the products that its handles. Although both go 
            hand in hand, there are differences between the way the two are 
            promoted. Nevertheless, the synergy between the corporate as a brand 
            and the product as a brand lend to the overall success of both. 
            There is a symbiotic relationship between the company and the brand. 
            The success of the brands builds the reputation of the company. 
            Conversely, a highly reputed company tends to rub off its reputation 
            on to its brands. In short, building your brand and building your 
            company name goes hand in hand. &quot;For example, in independent 
            surveys, the SBG has been rated the No. 1 business house and many of 
            its brands including Toyota have been rated No. 1 in their 
            respective segments. It is, therefore, no wonder that the SBG has 
            been steadily moving ahead a<img border="0" src="kr2.jpg" align="left" hspace="6" width="154" height="173">nd its brands enjoy a unique reputation 
            in the market,'' explains SBG's spokesperson. <br>
            <br>
            According to Rajiv Ahuja, communications head at Khimji Ramdas, &quot;The 
            issues involved in corporate branding are more than those in 
            branding a product. We let the ethos of our company reflect in our 
            corporate ads. Khimji Ramdas has been in this country since 1878. We 
            are a part of the country's social and cultural fabric and we let 
            that reflect in our ads. Corporate branding is not just a simple 
            advertisement. It is the experience that people have with us, 
            whether they are individual customers, agents, suppliers or 
            retailers. We have to live up to the ideals that reflect in our 
            corporate branding. It basically brings out the subtle needs.'' <br>
            <br>
            <img border="0" src="sbg.jpg" align="right" hspace="6" width="153" height="159">Suhail Bahwan Group's Mehta feels that &quot;corporate brands work as an 
            endorser for the products. Corporate brand stresses the set of 
            values of a company. The brand here looks more like a relationship, 
            it has to behave like a good friend with no schizophrenia, no 
            dramatic mood swings - something you can trust at all times. We, at 
            Suhail Bahwan Group, believe that acquiring consumer confidence, 
            building trust and demonstrating responsibility is very important. 
            Corporate branding plays a vital role in achieving this because it 
            clearly defines who we are and what we stand for. A successful 
            corporate brand is a highly valued asset that the best companies 
            build - for themselves, for their customers and the markets that 
            they serve.''<br>
            <br>
            <b><br>
            Post-WTO scenario </b><br>
            After the World Trade Organisation (WTO) regime sets in and the 
            Omani market opens up to more competition, how will it affect the 
            various established brands in the market? This is one point where 
            all corporates seem to agree. Contrary to the common belief that the 
            post-WTO market could be a shrunk one for established marketers of 
            today, the corporate world believes that the opened up market will 
            have much more to offer.<br>
            <br>
            Varghese is absolutely confident that the performance of Muscat 
            Electronics will not even go down by a quarter of a per cent. &quot;I do 
            not feel that there is any kind of threat that we face once WTO 
            regulations come into force and there are other distributors. We 
            would continue to be the distributor of Sony and other brands that 
            we currently have. Our relationship with our dealers is based on 
            trust and we have a wonderful rapport with all of them. However, 
            once the WTO regulations come into force and a common GCC market is 
            created, pricing will be very competitive. If we are not competitive 
            in terms of price, products will start overflowing from other 
            markets to Oman, but it will not threaten our distribution,&quot; he says 
            confidently. <br>
            <br>
            Pande gives wind to Varghese's statement. &quot;It will not have much of 
            an impact. As far as we are concerned, it is a non-issue. Problems 
            arise when a principal has problems with a distributor. In such a 
            case, it becomes an escape route for them, and they can deal with 
            other distributors. We have a long relationship with our principals 
            based on trust and respect. We offer them the best, so why should 
            they look for other distributors,&quot; he asks.<br>
            <br>
            The SBG feels that once other competitors come in, there will 
            certainly be challenge. However, they feel they have such a strong 
            presence that it will not deter their plans. Obviously, it takes a 
            huge amount of investment, trained manpower and a unique in-house 
            orientation to carefully build a brand and develop a large and loyal 
            customer base over the years. This automatically erects stiff entry 
            barriers for competitors and the group may not be wrong in believing 
            that it will be extremely difficult for a new entrant to replicate 
            it in the short term. <br>
            <br>
            Sums up the SBG's spokesperson: &quot;While we recognise that competitors 
            will constantly upgrade their skills and strategies, we shall 
            maintain our superiority and edge by constantly setting new 
            standards in product, service and customer care.'' When that 
            happens, though, one thing is for sure - the consumer is going to 
            have a good time with better products and services on offer at much 
            more competitive prices. As Al Kindi of AutoPlus puts it succinctly: 
            &quot;I'll have more choice!''</font></p>

