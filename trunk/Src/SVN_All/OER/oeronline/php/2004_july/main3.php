            <font face="Verdana" size="4" color="#FF0000">Innovate or perish</font><p align="justify">
            <font face="Verdana" size="1"><b>OER meets with Eng. Ahmed Al Dheeb, 
            executive president, PEIE, who talks about innovation, higher 
            productivity, collaboration, competition, sharing ideas, investing 
            in R&amp;D and Sir Richard Branson. Excerpts: </b><br>
            <br>
            <img border="0" src="interview.jpg" align="left" hspace="6" width="200" height="193"></font>P<font face="Verdana" size="1">ublic 
            Establishment for Industrial Estates (PEIE), the organisation 
            responsible for developing and managing prime industrial land and 
            high-tech office accommodation in eight locations across the 
            Sultanate, will be organising the Gulf Enterprise Meet (GEM) in 
            September this year in association with OER as its official media 
            partner. <br>
            <br>
            GEM is an eight panel, two-day event that will look at business 
            innovation and entrepreneurship. Indeed, the PEIE-organised 
            conference has attracted a number of leading business, academic and 
            industry speakers from the Gulf, Europe and the US. OER meets with 
            PEIE executive president Eng. Ahmed Al Dheeb: <br>
            <br>
            <b>What does innovation mean to you? </b><br>
            Innovation is the successful exploitation of new ideas. We want to 
            get ideas out of the research centres, into the offices and 
            factories and onto the balance-sheet to help Omani businesses 
            compete in the global market. To be frank, our future success will 
            be won through the exploitation of new ideas, particularly in the 
            area of information and communication technology. <br>
            <br>
            For the Omani economy as a whole, innovation is the key to higher 
            productivity and greater prosperity for all. The government has 
            already laid the foundations of an innovation-driven economy by 
            creating a stable macroeconomic environment, promoting fair and free 
            trade and improving education and skills. But there's more to be 
            done. To hold our own in modern manufacturing and ICT - areas that 
            PEIE is actively involved in - we'll need to innovate strongly by 
            creating new high-tech firms and light-manufacturing industries as 
            well as helping current PEIE-based firms upgrade. <br>
            <br>
            At the same time, we need to raise the level of innovation in our 
            service industries. In this regard, PEIE is in the vanguard of 
            helping Oman become a regional knowledge hub - a country with a 
            reputation for turning knowledge into new and exciting products and 
            services; a country that invests in business R&amp;D and education and 
            skills, and exports value-added goods and services. As you're aware, 
            Knowledge Oasis Muscat is very much part of this process. <br>
            <br>
            Indeed, this is why PEIE is organising GEM - we feel there's a real 
            need to bring business people, policy makers and academics together 
            on an annual basis to discuss innovation and entrepreneurship. In 
            fact, what I find really exciting about GEM is that delegates will 
            hear from a host of European, US and Gulf-based companies that have 
            reaped the benefits of innovation. In my view, business must raise 
            its sights, collaborate, compete, share ideas, invest in research 
            and development and also take responsible risks. These are some of 
            the messages we want to get across at GEM. <br>
            <br>
            </font><b><font face="Verdana" size="1">We hear a lot about the 
            Knowledge Economy - what's your take on it? </font></b>
            <font face="Verdana" size="1"><br>
            In my view, the knowledge economy is shorthand for the changes 
            taking place in markets across the globe. Just think back a little, 
            only 20 years ago, phone conversations travelled by copper wires 
            which carried less than one page of information per second. Today, a 
            strand of optical fibre as thin as a human hair can transmit in a 
            single second the equivalent of over 90,000 volumes of an 
            encyclopaedia. This change is driven by two factors which reinforce 
            each other - rapid development of technology, and the opening up of 
            markets across the globe. <br>
            <br>
            <b>When you look around, who's being innovative? </b><br>
            Let's look at the PC business. For instance, you have traditional 
            players producing and selling computers in a certain way. Out of the 
            blue comes Dell, selling computers direct. What Dell has done is to 
            discover a new who-what-how-positioning in the business, one that 
            allows them to break the existing rules of the game. Now that's 
            innovative. <br>
            <br>
            <b>Are there any established firms that are innovative? </b><br>
            Most innovators are new entrants or start-ups. For instance, EasyJet, 
            Amazon.com, Starbucks and Applied Voice &amp; Speech Technologies (AVST) 
            spring to mind. These were start-ups that, having identified a new 
            positioning, pursued it rigorously. It is unusual to find an 
            established company innovating. And there are many reasons for that, 
            the most important being that established companies already have a 
            positioning that enables them to make money; so why look for any 
            other positioning? A new entrant, on the other hand, starts with a 
            clean canvas, allowing it to identify a position that hasn't been 
            taken yet. Another obstacle that established firms face is the 
            inertia of success. Well-established, with a solid market share, 
            reputed and highly profitable... why should they change? Then there 
            is the problem of identifying a new positioning. If it was that 
            easy, everybody would be doing it, wouldn't they? <br>
            <br>
            <b>How does an established company actually go about it if it wants 
            to innovate? </b><br>
            There's no right way of going about this. But there are a variety of 
            tactics a company can use to improve the probability of producing 
            new ideas. One is to fundamentally question the existing mental 
            models of the organisation. Let's face it, every organisation 
            operates under certain mental models and paradigms. For example, 
            these are our customers, this is how we market, this is how we sell, 
            this is how we do business... What I'm suggesting is that an 
            organisation will never discover anything new unless it first 
            questions what it already has and says. Ask yourself - why are we 
            doing it like this, is there another way? As long as an organisation 
            is happy and satisfied with what it has, it isn't going to search 
            for something new - and will never discover anything new as a 
            result. <br>
            <br>
            <b>With your experience of setting up KOM, where do you see 
            innovation happening in the future, particularly in IT?</b> <br>
            Probably in places we least expect it. I'm intrigued by what's going 
            on in countries like China that are promoting Linux and other open 
            source solutions in its government and to its citizens. Their 
            government favours Chinese-developed open source software, such as 
            the Chinese Academy of Science's Red Flag Linux. Like many countries 
            around the world, China wants to build its ICT infrastructure and 
            economy with domestic spending and expertise. India, Argentina, 
            Mexico, Germany, Venezula and Peru are doing similar things, if not 
            at the government level, then at the grassroots and corporate level. 
            With all these people building software to solve real-world problems 
            using open source tools and technologies, I expect there will be a 
            deluge of new innovation in that space in the next ten years or so.
            <br>
            <br>
            <b>What do you feel are the main challenges facing Gulf-based small 
            businesses today? </b><br>
            One of the main challenges facing the region's small businesses 
            today centres around the death of distance. With improving 
            technology and the worldwide web, small businesses can operate on a 
            global basis. For this to succeed, they need to be as professional, 
            as efficient and as cost-effective as possible. The other challenge 
            facing them is the immense competition from larger companies. <br>
            <br>
            <b>What's the state of entrepreneurialism in the Gulf? </b><br>
            Entrepreneurialism is thriving in the region, but there is a high 
            churn rate of entrepreneurs who come and go. For instance, hundreds 
            of budding entrepreneurs will give it a go this year in terms of 
            setting up their own enterprise but many of these will close within 
            their first three years. This is one of the many start-up issues 
            we're grappling with in the Knowledge Mine, the business incubator 
            program at KOM. <br>
            <br>
            <b>You've got a broad business background - what type of person do 
            you think makes a good entrepreneur? </b><br>
            Being a bit of a loner tends to help, as sometimes the whole world 
            is against you. You could go into work one morning and find a bank 
            breathing down your neck, a supplier wanting to know what's 
            happening, orders stacking up and staff not turning up. So, a good 
            entrepreneur needs to have a thick skin. <br>
            <br>
            <b>A lot of people think entrepreneurs are born, not made. What's 
            your take on that?</b><br>
            This is an interesting question. I'd say both. Some people are born 
            as such - they have the right temperament and are entrepreneurial 
            from an early age and others pick it up as they go along. For people 
            like Steve Jobs of Apple and Sir Richard Branson of Virgin, I'd 
            think it's more the former than the latter. I admire people like 
            Jobs and Branson for creating new things in the face of severe doubt 
            and opposition, and in doing so, for making the lives of people that 
            much better. Same for HP in its early days. I admire Bill Joy for 
            coming up with Java and Sun for maintaining a culture of innovation 
            and entrepreneurship. <br>
            <br>
            <b>In your role as PEIE's Executive President, what are the key 
            innovation lessons you've learnt over the years? </b><br>
            You can't delegate entrepreneurship - if you're going to make 
            mistakes, make them yourself. Pay attention to the detail of a 
            business, those mistakes in the business model may be small now, but 
            they're compounded by growth. Don't overestimate what consumers will 
            pay for something that saves them time. Don't risk your shirt on the 
            business, put enough in to have enough skin in the game, an amount 
            that is meaningful but leaves an amount small enough for you to 
            survive. If you could change one thing overnight to make it easier 
            to start up an enterprise, what would it be? Changing attitudes to 
            enterprise and innovation is the key to this - fear of failure is 
            one major factor that prevents many people--young and old--from 
            putting their business ideas into practice and business survival 
            rates improve dramatically when fledgling businesses seek advice and 
            support. So, my wish is for a cultural change to take place in the 
            region to stimulate more business start-ups, with people taking 
            advice at each stage leading to more successful, growing businesses, 
            following on from that.</font></p>

