<p align="justify"><font face="Verdana" size="1"><b>Outlook </b><br>
The MSM Index has been re-rated and currently stands at more than 14 times on 
trailing 12-month earnings basis. However, this is still undervalued as compared 
to other GCC Indices. Further, the overall dividend yield based on the dividends 
announced for 2003 is still higher at 4.5 per cent, a significant difference 
than the miserly rates offered for deposits across the GCC markets. Hence, we 
believe that this inequality will continue to fuel the markets in the short 
term. Importantly, this time round, the rally is based on actual cash being 
invested in the markets as both the CMA as well as the banking community have 
taken adequate precautions of not repeating the mistakes committed during the 
MSM stock market bubble of 1997.</font></p>

