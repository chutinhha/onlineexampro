<p align="justify"><font face="Verdana" size="4" color="#FF0000">MSM<br>
<br>
</font><font face="Verdana" size="2" color="#FF0000"><b>In overdrive</b></font></p>

<p align="justify"><font face="Verdana" size="1"><b>Fincorp examines the latest 
stock movements on the Muscat Securities Market as the re-based MSM Index scales 
new heights </b>
</font></p>

<p align="justify">M<font face="Verdana" size="1">SM climbed to newer heights 
during the period under review. In just under one month, the Index has gained by 
more than 5.5 per cent to touch 336 levels, last seen during July 1998. All the 
sectors gained handsomely, with Industry sector dominating the May month of the 
period under review while Banking stocks took the honours during the initial 
fortnight of June. Till OER went to Press, the Index had further zoomed up and 
now eyes the mystical 350-mark. In the meantime, MSM Indices got rebased to the 
thousand levels beginning from June 1, 2004. This is to bring the Index in line 
with the GCC indices bases for a better regional focus.
</font></p>

<p align="center"><img border="0" src="msm1.jpg" width="369" height="119"></p>

<p align="center"><img border="0" src="msm2.jpg" width="369" height="422"></p>

<p align="left"><font face="Verdana" size="1">
<b>Banking scrips to the fore </b><br>
To start with, if the month of May belonged to cement companies viz. Raysut &amp; 
Oman Cement, then with same certainty, the initial fortnight of June 2004 
belonged to the Banking Sector viz. BankMuscat &amp; Oman International Bank (OIB). 
Stocks of both banks gained by around 10 per cent during the period, helping the 
Index cross the crucial 322-mark. Both stocks are now quoting at a historically 
high valuation of 12.66 times and 14.8 times, respectively, based on their 
annualised first quarter earnings. <br>
<br>
This further fuelled the rally across the markets, with holding and investment 
companies off to a flying start. This included Muscat National Holding (which 
has OIB shares), Oman &amp; Emirates (BankMuscat), Oryx JIA (BankMuscat and NBO), 
Majan JIA (NBO and BankMuscat), First Mazoon (OIB) etc. These gains rubbed off 
on other banking stocks--BankDhofar and NBO--too.
</font></p>

<p align="justify"><font face="Verdana" size="1">
<b>New issues</b> <br>
Al Maha Petroleum Products debuted on June 15 and since then has been sharing 
the limelight among the Banking heavyweights. The stock debuted at RO3.600, a 
straightway gain of more than RO1 excluding the cash dividends; reached the 
magical figure of RO4 before settling at around RO3.900 till our last coverage. 
&quot;<br>
<br>
A new IPO -- Dhofar University -- also hit the markets on June 16. The size of 
the issue is RO3.57 million and is prompted by listed companies like DIDIC, 
Dhofar Insurance, BankMuscat, Dhofar Cattlefeed and Raysut Cement. However, the 
university is expected to start its initial intake of students during September 
2004. <br>
<br>
Among the yearly results declared for March-ending were Oman Fisheries and OIFC. 
Oman Fisheries announced a yearly profit of RO1.05 million, thereby earning its 
shareholders around 82 baiza per share. Currently, the stock is being discounted 
at 17 times. Similarly, OIFC announced lower than expected results due to the 
provisions with a net profit of RO1.78 million along with a dividend of 20 per 
cent. Overall, it is getting traded at around eight times its yearly earnings.
</font></p>

