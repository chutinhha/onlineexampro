<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">An 
Mission Possible</font></font><font face="Verdana" size="1"><br>
<br>
<b>Matt Harrison outlines the raison d'être of mission statements, provides the 
right elements for making one, and argues in favour of having a pithy statement</b><br>
<br>
<b>
<img border="0" src="im7.jpg" align="left" hspace="5" width="147" height="191">Mission 
Statement – Write One!</b><br>
Crafting a mission statement is one of the things business people find hardest 
to write; they can tell a lot about your organisation, so it’s important to take 
time and put effort into writing a good one. In broad terms, think of a mission 
statement as a cross between a slogan and an executive summary. Just as slogans 
and executive summaries can be used in many ways, so too can a mission 
statement. An effective mission statement should be able to tell your 
organisation’s story and ideals in less than 30 seconds.<br>
<br>
In both the public and private sector, a meaningful mission statement helps keep 
people on track and aimed in the right direction. Simply put, it describes the 
purpose of the organisation. In order for any outfit to succeed, it’s essential 
that it have clear and concise direction. A mission statement will provide a 
significant part of that direction by serving as a constant reminder of purpose 
and ultimate goals.<br>
<br>
<b>Enduring purpose</b><br>
Your mission statement should be an enduring statement of purpose, one that 

identifies the scope of your operations in product and market terms, and 
reflects your values and priorities. A carefully drafted mission statement 
should state with clarity and precision your goals, purpose and strategy. In 
brief, it should answer the questions: “Why does this organisation exist and 
what does it hope to achieve?” Indeed, failure to clearly state and communicate 
your mission can have harmful consequences, including:<br>
• Wasting time ‘barking up the wrong tree’.<br>
• Not thinking broadly enough about different possibilities especially if its 
mission statement is unclear or overly narrow. <br>
• Not realising when you’ve achieved your objectives.<br>
<br>
<b>Right ingredients</b><br>
It’s generally accepted that a mission statement should consist of three key 
ingredients: <br>
<br>
<b>1: The Purpose Statement</b><br>
The purpose statement clearly states what you seek to accomplish: Why you exist. 
What’s the ultimate result of your work? Purpose statements usually include two 
phrases:<br>
• An infinitive that indicates a change in status, such as to increase, 
decrease, prevent, eliminate, attract, develop.<br>
• An identification of the problem or condition to be changed.<br>
<br>
<b>2: The Activity Statement</b><br>
This statement outlines your activities or programs. Specifically, you must be 
able to answer: ‘What activity are we going to do to accomplish our purpose?’<br>
<br>
<b>3: The Values Statement</b><br>
Values are beliefs, which you and your employees hold in common and endeavour to 
put into practice; they guide you all in performing your day-to-day activities. 
Specifically, you need to ask: ‘What are the basic beliefs that we share as an 
organisation?’ Examples of these might include: a commitment to excellent 
services, innovation, diversity, creativity, honesty and integrity.<br>
In addition to these three elements, you also need to address the following 
questions when developing your mission statement:<br>
1. What is the problem or need your organisation is trying to address?<br>
2. What makes your organisation unique?<br>
3. Who are the beneficiaries of your work?<br>
So, a mission statement is an essential tool in making sure that your 
organisation and employees are all in agreement on what you’re trying to do and, 
more importantly, what you aren’t trying to do.<br>
<br>
<b>The big boys</b><br>
All the big boys have mission statements. If Xerox, Apple, IBM, General Electric 
and Hewlett Packard have them, then so should Oman-based organisations, large 
and small. Doing so will lead to greater focus and direction of purpose. <br>
<br>
As David Packard, co-founder of Hewlett Packard, remarked: “When the entire 
team—from management to production—are working together in a co-ordinated, 
co-operative manner, significant strides will be made in achieving the 
organisation’s purpose and goals.” Here are four examples from the large 
corporates: <br>
<br>
• Boeing’s mission is “to become the number one aerospace company in the world 
and among the premier industrial concerns in terms of quality, profitability and 
growth.” <br>
• For decades Avis’ slogan has been “We Try Harder,” but now makes it clear that 
its mission is “total customer satisfaction.” <br>
• Lowe’s mission statement tells its purpose and goals as being “in the business 
of providing the products to help our customers build, improve and enjoy their 
homes. Our goal is to out-service the competition and be our customers’ 1st 
choice store for these products.” <br>
• General Electric’s unique, thought provoking mission statement consists of 
three words: “Boundaryless . . . Speed . . . Stretch.”<br>
<br>
After the mission statement has been crafted, the next step is communicating the 
vision of your organisation—its product or services—to your employees and 
getting them on board. Packard said: “It’s necessary that people work together 
in unison toward common objectives and avoid working at cross purposes at all 
levels if the ultimate in efficiency and achievement is to be obtained.” <br>
<br>
His original mission statement has been expanded into HP’s Corporate Objectives, 
which include customer loyalty, profit, market leadership, employee commitment, 
leadership capability and global citizenship.<br>
<br>
<b>Key ingredients</b><br>
As a quick re-cap, here are some basic guidelines in writing your mission 
statement: <br>
• Say who your organisation is, what you do, what you stand for and why you do 
what you do. <br>
• Input from all members of an organisation make for the development of a more 
effective mission statement. <br>
• Although there are exceptions, the best mission statements tend to be 3-4 
sentences in length. <br>
• Avoid saying how great you are and what fantastic quality and service you 
provide. <br>
• Research other mission statements, but make sure your statement is you and not 
somebody else! <br>
• Believe in your mission statement – if you don’t, it’s a lie and your 
customers and employees will soon realise it. <br>
<br>
<b>Where do I go?</b><br>
In brief, creating a clear and concise mission statement isn’t an easy task. 
However, the results of a well-conceived one will be well worth the investment 
of time. Indeed, the critical importance of a mission statement is summarised 
eloquently by Lewis Caroll through the words of the Cheshire Cat in Alice in 
Wonderland: ‘‘If you don't know where you’re going, it doesn’t matter which way 
you go.’’<br>
