                    Refinery signs deal to up output </b><br>
                    Oman Refinery Co. LLC (ORC) signed an $86.8 million (RO33.38 
                    million) contract with Oman Contractors Union Co. to upgrade 
                    the 85,000-barrels-a-day Mina al-Fahal oil refinery to 
                    increase its gasoline production. <br>
