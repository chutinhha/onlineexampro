                    <p align="justify"><font face="Verdana" size="1"><b>OWSC 
                    begins work for phase-I</b><br>
                    Oman Wastewater Services Company (OWSC) has announced the 
                    commencement of phase one of the wastewater project, which 
                    will cover areas from Hilat Al Sad to Al Quturn, MSQ Madinat 
                    Al Elam, Al Khuwair, Al Ghubra until Al Athaiba. The first 
                    phase of the project will connect more than 18,000 
                    residential and commercial buildings to the new wastewater 
                    networks, which is to serve more than 150,000 people. <br>
                    <br>
                    The construction phase reflects the most important part of 
                    the wastewater project since it will take place in the most 
                    populated areas that require wide and extensive excavation 
                    works and cutting of roads, said Eng. Omar Al Wahaibi, 
                    general manager, Oman Wastewater Services. Phase I will take 
                    roughly a period of three years to complete. The first two 
                    years of the project would focus on installing high quality 
                    pipe network of the wastewater system.<br>
                    <br>
                    The company is to form an emergency committee to deal with 
                    any incident that might result from the digging works. This 
                    committee would include Muscat Municipality; Oman 
                    Telecommunications Company (Omantel); Ministry of Housing, 
                    Electricity and Water; Royal Oman Police; and the company 
                    implementing the project. <br>
                    <br>
                    OWSC was set up to build a sewage project for Muscat. First 
                    drawn up in 1996, the project is part of Oman’s efforts to 
                    privatise public utilities. Work was initially due to start 
                    in July 2001 by a consortium led by Galfar Engineering and 
                    Contracting and BankMuscat, but the withdrawal of some 
                    partners has led the government to proceed with its own at a 
                    revised cost of $260 million (RO100 million) compared with 
                    an initially planned $600 million (RO230.77 million).</font></p>
