                    <p align="justify"><font face="Verdana" size="1"><b>Pact of 
                    consultancy services on e-government laws signed </b><br>
                    An agreement to provide consultancy services for the 
                    development of e-legislation to support Oman digital society 
                    and e-government initiative has been signed. The agreement 
                    was signed between HE Mohammed bin Nasser Al Khusaibi, 
                    secretary-general at the Ministry of National Economy and 
                    head of the executive committee following up implementation 
                    of the national information technology strategy with Said Al 
                    Shahry Law Office, which is in partnership for this project 
                    with the law firm Baker &amp; McKenzie. <br>
                    <br>
                    The agreement will help in furthering the government’s 
                    efforts in making Oman a digital society. Besides, it will 
                    help in pushing the country’s e-government plans which will 
                    help in delivering government services to citizens and the 
                    business sector, like validity of electronic contracts, 
                    roles, duties and liabilities of electronic intermediaries, 
                    privacy and security of information and consumer protection 
                    in electronic transactions. <br>
                    <br>
                    It provides for formulating in Arabic and English languages 
                    laws that regulate electronic transactions and also for 
                    comprehensive revision to the existing body of legislation 
                    to accommodate requirements of both the e-government and the 
                    broader digital society.</font></p>
