                    <font face="Verdana" size="1"><b>Fall in international 
                    telecom rates<br>
                    </b>Oman Telecommunications Company (Omantel) announced more 
                    reductions in the international telecom tariff with effect 
                    from March 1, 2005, recently. Engineer Mohammed bin Ali Al-Wohaibi, 
                    executive president of Omantel, explained that the revised 
                    rates offer a reduction of up to 33 per cent in 140 
                    countries with significant international traffic worldwide. 
