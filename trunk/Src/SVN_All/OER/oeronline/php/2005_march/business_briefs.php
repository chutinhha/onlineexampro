                    Mobile calls including Hayyak, Jibreen, Sahl and Al-Multaqa 
                    (Payphone services). <br>
                    <br>
                    Al-Wohaibi further said that the major objective of the 
                    fresh reduction is to make the rates nationally, regionally 
                    and globally competitive and affordable to all communities 
                    in the Sultanate and to provide greater relief to corporate, 
                    government and SMEs to have cheaper means of communication, 
                    which will undoubtedly support the economic growth.<br>
                    <br>
                    The Omantel executive president also stressed that there 
                    will be a change in pulse rate, which will provide further 
                    relief to callers. The Fixed Telephone and Mobile (including 
                    Hayyak) services being billed for 1 full minute even for a 
                    call duration of 1 second will be revised to 30 seconds 
                    pulse, after the first full minute. </font></p>

