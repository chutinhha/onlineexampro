                    The project will enable the plant to produce more gasoline 
                    from condensate processing, the agency quoted ORC chief 
                    executive Abdulaziz al-Kindi as saying. The overall aim is 
                    to meet domestic gasoline demand as well as reduce imports 
                    of the gasoline additive methyl tertiary-butyl ether, which 
                    is used in the production of unleaded gasoline. <br>
                    <br>
                    The project will boost the refinery’s processing capacity to 
                    106,000 b/d when the project completed at end of 2006. Mina 
                    al-Fahal is the only refinery now operating in Oman, while 
                    the Sohar refinery, which is under construction, is expected 
                    to be operational in the first half of 2006. </font></p>
