<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Investing 
in Oman</font></font><font face="Verdana" size="1"><br>
<br>
<b>Michael Earley provides an overview of the fundamental aspects of the laws 
affecting foreign investment in Oman</b><br>
<br>
<img border="0" src="im9.jpg" align="left" hspace="5" width="125" height="146">Since 
its accession to the World Trade Organisation (WTO), Oman has made significant 
progress in liberalising its legislation to promote foreign participation and 
investment in the local market. Amendments to the Foreign Capital Investment Law 
promulgated by Sultani Decree 102/94 now allow an unprecedented level of foreign 
participation in companies in Oman. This, combined with the increased focus on 
tourism as evidenced by the establishment of the Ministry of Tourism, means that 
the opportunities for foreign investment in Oman have never been better. <br>
<br>
Although a comprehensive review of the laws affecting foreign investment in Oman 
is beyond the scope of this article, there are several fundamental aspects of 
the legal regime with which a potential foreign investor may be interested.<br>
<br>
<b>Commercial Companies and Foreign Investment in Oman</b><br>
The Oman legislation on commercial companies broadly reflects the European 
approach to company law, and the 6 corporate forms available under the Oman 
Commercial Companies Law (&quot;CCL&quot;), promulgated by Sultani Decree 04/74, have 
their origin in those encountered in European civil law jurisdictions, 
particularly France. <br>
<br>
The Foreign Capital Investment Law (&quot;FCIL&quot;), promulgated by Sultani Decree 
102/94, governs foreign investment and the participation in a company in Oman by 
foreign entities. For the purposes of the FCIL, a company with non-Omani and 
Omani shareholders is classified as a &quot;mixed&quot; commercial company. The FCIL 
provides that non-Omani natural or juristic persons are prohibited from engaging 
in any commercial, industrial, or other business in Oman without first obtaining 
a foreign capital investment licence from the Ministry of Commerce and Industry. 
The minimum capital requirement for a mixed company is RO150,000.<br>
<br>
According to the FCIL, there are three levels of approval for a company with 
foreign shareholders, depending on the amount of the foreign shareholding. The 
first level is approval by the Ministry of Commerce &amp; Industry (&quot;MCI&quot;) where the 
foreign ownership does not exceed 49 per cent of the capital of the company. The 
second level is approval by the foreign capital investment committee where the 
foreign ownership is up to 65-70 per cent of the company’s capital. <br>
<br>
The third level is approval by the Council of Ministers where the foreign 
element exceeds 70 per cent of the capital of the company. Whilst the FCIL 
provides for the possibility of 100 per cent foreign shareholding in a 
commercial company, approval for such participation is rare where the foreign 
entity is not from a GCC country.<br>
<br>
If a foreign entity does not wish to participate in a mixed commercial company, 
it may choose instead to establish a branch office. However, branch office 
status is generally reserved for those foreign entities that have entered 
directly into contracts with the Government of the Sultanate of Oman, and the 
activities in which they may participate will be limited to those specifically 
provided in the contract.<br>
<br>
<b>Taxation</b><br>
Commercial companies in Oman are generally taxed at the same rate, unless the 
foreign shareholding exceeds 70 per cent of the capital of the company. For 
companies with a foreign shareholding of 70 per cent or less, the first RO30,000 
of profit is exempt from taxation, and any amount beyond that is taxed at 12 per 
cent. Branches and companies with more than a 70 per cent foreign shareholding 
are taxed in bands of up to 30 per cent of their profits, depending on the 
amount of profits generated.<br>
<br>
<b>Land Ownership</b><br>
The current land laws in Oman proscribe the ownership of land by non-Omani 
natural or juristic persons. Accordingly, neither a commercial company 
established pursuant to the FCIL, nor a branch, may own land in Oman. In 
instances where the activities of a mixed company contribute to the development 
of the national economy, the company may obtain a usufruct right from the land 
owner for up to 50 years (this term may be renewed). A usufruct right is a real 
right, limited in time, expiring either on the date provided in the usufruct 
agreement, or upon the death of the person to whom the usufruct right was 
granted (&quot;usufructuary&quot;). During the term of the usufruct, the usufructuary is 
entitled to use the land as an owner, but upon termination of the usufruct must 
restore the land to its original condition. Alternatively, mixed companies may 
freely lease land from land owners.<br>
<br>
Although Oman has recently promulgated a Ministerial Decision (&quot;MD&quot;) purporting 
to liberalise the land ownership regime to allow foreign natural and juristic 
persons to purchase land in Oman under limited circumstances, no Sultani Decree 
has been promulgated to support such a policy. Consequently, a foreigner's right 
to own land in Oman pursuant to the MD remains unclear. Whilst this MD may be 
the first step toward the liberalisation of land ownership in Oman, it is 
expected that future legislation will be required before foreigners can enjoy 
land ownership rights in Oman.<br>
<br>
<b>Omanisation</b><br>
In order to support the local employment market, Oman has adopted a government 
mandated Omanisation policy whereby a specific percentage of employees of the 
companies in certain industries are required to be Omanis. Although many 
technical and labour related areas are not yet subject to Omanisation, new 
legislation is continually being introduced to extend this policy to additional 
industry sectors.<br>
<br>
In light of Oman's shifting economic focus, many foreign companies have enjoyed 
the fruits of significant investments in Oman. As the promulgation of new laws 
continues to refine the regime for foreign investment, there seems little doubt 
that Oman's diversification plans are likely to result in greater foreign 
investment with reasonable expectations of favourable returns on those 
investments.<br>
<br>
