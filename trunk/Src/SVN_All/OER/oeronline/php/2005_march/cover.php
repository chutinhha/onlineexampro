            <p align="justify"><font face="Verdana" size="1"><b>Overview of the agreement</b>
            <br>
The FTA aims at achieving extensive liberalisation
across a wide spectrum of
trade issues, both in terms of goods and
services. Following are some of the highlights
of the US-Bahrain FTA:
<br>
            <br>
            Liberalisation of trade in goods
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">96 per cent of Bahrain industrial and
agricultural products will receive
immediate duty-free access to the US
markets upon entry to force of the
Agreement.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Tariffs on the remaining products,
which are not currently produced in
Bahrain, will be phased-out according
to the following staging categories:
<br>
            <b>Category A:</b> Immediate duty-free
access (96 per cent of industrial and
agricultural products).
<br>
            <b>Category B:</b> Duties will be eliminated
in 10 equal annual stages (1 per
cent of industrial and agricultural
products).
<br>
            <b>Category C:</b> Goods that are already
duty free and will continue to receive
duty-free treatment.
<br>
            <b>Category D: </b>Duties will be eliminated
in 5 equal annual stages. (3 per
cent of industrial and agricultural
products).
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">All US industrial and agricultural
products will receive duty-free
access, except 80 products of which
the duties will be phased-out over 10
years.
<br>
            <br>
            <b>Textiles and apparel
<br>
            </b></font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Textiles and apparel products will
receive immediate duty-free access
upon entry to force of the agreement,
as long as they meet the rules of origin
requirement (Yarn-Forward Rule).
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement provides for a 10-
year transition period, during which
Bahrain’s production of textiles and
apparel that do not meet the rules of
origin requirement will be exported
to the U.S. duty free (Limited to 65
Million Square Meters).
<br>
            <br>
            <b>Customs administration</b> <br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement includes commitments
to ensure transparent and efficient
customs administration, including
the publication of laws and regulations
on the Internet and establishing
prompt customs clearance procedures.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Both Governments agree to share
information to combat the illegal
trans-shipment of goods and implement
special customs cooperation
measures to prevent fraud.
<br>
            <br>
            <b>Rules of origin</b> <br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The agreement lays
out specific rules of origin
to ensure that only
Bahrain and US goods
benefit from the preferential
duty treatment of
the Agreement.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Two types of goods
produced in Bahrain
will receive the benefits
of the agreement:<br>
            <b>Goods originating in
Bahrain</b> <br>
Goods that underwent a substantial
transformation with at least 35 per
cent value added.<br>
            <b>Commitments on
Technical Barriers to
Trade (TBT) and
Sanitary and Phyto-sanitary
Measures (SPS)
<br>
            </b></font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement ensures
that policies and regulations
on food labelling,
biotechnology, and shelf life
standards are transparent,
science-based, and are in accordance
with international standards.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Reaffirms Bahrain’s WTO commitment
on TBT and SPS measures and
ensures that technical regulations,
standards, and conformity assessment
procedures are not an impediment
to trade.
<br>
            <br>
            <b>Government procurement
<br>
            </b></font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement reinforces rules of
fairness, transparency, and predictability
of government procurement
procedures, such as advance
public notice of purchases and timely
and transparent bid decision-making
and review procedures.<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Bahraini suppliers will be granted
National Treatment and non-discriminatory
access to bid on contracts
from various U.S. Central
Government entities and government-
owned enterprises. Similarly,
US suppliers will be granted access
on a non-discriminatory basis to bid
on contracts from various Bahrain
government entities.
<br>
            <br>
            <b>Services and cross-border services</b> <br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Service providers from each country
will enjoy substantial market access
to the services market of the other
country and will be accorded
National or Most-Favoured Nation
Treatment.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement uses the “Negative
List” approach, meaning that all sectors
are covered unless specifically
excluded.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Key services sectors covered by the
Agreement include telecommunications,
computer and related services,
healthcare, education, advertising,
tourism, transportation, engineering,
construction, audiovisual, and
express delivery services.
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement provides benefits for
businesses wishing to supply services
cross-border (e.g. electronically),
as well as businesses wishing to
establish local presence in the other
party’s territory
<br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">All policies and measures affecting
services trade will be applied in a
fair, objective, and non-discriminatory
manner.
<br>
            <br>
            <b>Financial services</b> <br>
            </font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The chapter covers traditional as well as Islamic financial services.
            <br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Financial service providers from
each party will enjoy access to the
other party’s financial services markets
and be accorded National or
Most Favoured Nation Treatment,
except when excluded by a Non-
Conforming Measure.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Liberalisation of the Bahrain Stock
Exchange.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Liberalisation of Bahrain’s insurance
market.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Commitment to reaffirming legislative
and procedural transparency in
the financial services sector.
<br>
            <br>
            <b>Telecommunications </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Telecommunications service
providers from each country will
have access to the telecommunications
market of the other country.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">No restrictions on the type of
technology used.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government commits that
users of the telecommunications
network will have reasonable and
non-discriminatory access to the
network.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Firms of each party seeking to
build a physical network in the
other party’s territory will have
non-discriminatory access to facilities.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement includes a dispute
settlement mechanism in accordance
with the Bahrain Telecommunications
Law.
<br>
            <br>
            <b>Electronic Commerce </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement promotes a liberalized,
non-discriminatory trade environment
for E-Commerce.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government commits to non discriminatory
treatment of digital
products and agrees not to impose
customs duties on digital products.
<br>
            For digital products delivered on
hard media (such as a DVD or CD),
customs duties will be based on the
value of the media (for instance, the
disc), not on the value of the movie,
music or software contained on the
disc.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The E-Commerce commitments will
help develop Bahrain’s E-Commerce
infrastructure and promote Bahrain
an E-commerce leader in the Gulf
Region.<br>
            <br>
            <b>Intellectual Property Rights </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement covers the most
updated international standards for
copyrights, patents, and trademark related
commitments to lock-in intellectual
property protections and
enforcement to deter counterfeiting
and piracy.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Builds on the WTO Agreement on
Trade-Related Aspects of Intellectual
Property (TRIPS) and others such as
the World Intellectual Property
Organisation (WIPO) Copyright
Treaty, the WIPO Performances and
Phonograms Treaty, and the Patent
Cooperation Treaty.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Includes commitments to strengthen
Bahrain’s enforcement procedures,
provides for compensation of rights
holders for infringements, and provides
for criminal penalties for violators
under Bahraini law.
<br>
            <br>
            <b>Labour Rights </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government reaffirms its obligations
as members of the
International Labour Organisation
(ILO), and commits to strive to
ensure that its laws provide for the
highest labour standards.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government will be required to
effectively enforce its labour laws.
The Agreement makes clear that is
inappropriate to weaken or reduce
domestic labour protections to
encourage trade or investment.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Procedural guarantees to ensure
workers and employers access to
fair, equitable, and transparent

labour tribunals or courts.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">A Labour Cooperation Mechanism is
established to promote higher labour
standards and to further advance
common commitments, including
the principles embodied in the June
1998 Declaration on Fundamental
Principles and Rights at Work, and
compliance with ILO Convention
182 on the Worst Form of Child
Labour.
<br>
            <br>
            <b>Environment</b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government is required to
effectively enforce its environmental
laws.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Each government commits to establish
high levels of environmental
protection, and to not weaken or
reduce environmental laws to attract
trade or investment.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Procedural guarantees that ensure
fair, equitable and transparent proceedings
for the administration and
enforcement of environmental laws
are married with provisions that promote
voluntary, market-based mechanisms
to protect the environment.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Parallel to the Agreement, a
Memorandum of Understanding
was signed that will establish a Joint
Forum on Environmental
Cooperation to strengthen Bahrain’s
capacity to protect the environment
through the promotion of sustainable
development<br>
            <br>
            <b>Transparency </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">The Agreement requires that 
            trade related
laws and regulations are published
and that proposed regulations
are, to the extent possible, published
in advance and are open to public
commentary.

</font><font face="Marlett" size="1"><br>
            8</font><font face="Verdana" size="1">Each government is required to
apply fair procedures in administrative
proceedings covering trade and
to ensure that traders from the other
country can obtain prompt and fair
review of final administrative decisions
affecting their interests.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1">Commitment to prohibiting corrupt
practices, such as bribery, enforce
such prohibitions, enforce penalties
for violators, and adopt or maintain
measures protecting whistle -blowers.
<br>
            <br>
            <b>Enforcement of the agreement </b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1"> The Agreement emphasizes compliance
through consultation and trade enhancing
remedies.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1"> The Agreement provides for mechanisms
of state-to-state consultations
and dispute settlement procedures.
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1"> Commitment to efficient and transparent dispute panel procedure,
including open hearings, public
release of legal submissions by governments,
and opportunities for
interested third parties to provide
their input.
<br>
            <br>
            <b>Technical assistance
</b><br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1"> Parallel to the Agreement, the US has
committed itself to providing technical assistance to the Kingdom of
Bahrain in order to facilitate the
implementation and effective administration
of its FTA commitments
<br>

</font><font face="Marlett" size="1">8</font><font face="Verdana" size="1"> Technical Assistance takes various
forms, including market studies,
business development programs,
training of personnel, development
of electronic systems, assistance in
the development of policy manuals,
and legislative and procedural assistance
<br>
