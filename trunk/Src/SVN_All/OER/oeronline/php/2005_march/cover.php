            <b>Oman: Anatomy of a deal</b> <br>
Oman became the 139th member of the WTO on November 9, 2000. In acceding,
Oman made a number of important
modifications to its trade regime, dismantling
barriers to goods and services.
Yet, barriers remain, particularly for
investment and in some services and
goods sectors.
<br>
            <br>
            The March 2002 Monterrey
Consensus, reached at the United
Nations’ International Conference on
Financing for Development, emphasised
that countries need to attract investment
inflows through the development of a
“transparent, stable and predictable
investment climate, with proper contract
enforcement and respect for property

rights” and of “economic
policy and regulatory frameworks
for promoting and
protecting investments.”
<br>
            <br>
            Strong investor protections
are also critical to foster
the rule of law, to reduce corruption
and build institutions,
to promote respect for
and the protection of private property
and contract rights, and to create a regulatory
environment hospitable to capital
formation in general and international
investment in particular. Without these
protections, foreign investment will simply
not flow to the countries that need it
most.<br>
            <br>
To put things in perspective, here are
a few facts: US exports to Oman—
including machinery, aircraft, vehicles,
and electrical machinery—totalled just
$323 million (RO124.23 million) in 2003 –
half the amount the US exported to
Haiti. US exports of agricultural products
to Oman were $13 million (RO5 million),
including sugars, sweeteners and
beverage bases, and vegetable oils. US
goods imports from Oman in 2003 were
$695 million (RO267 million), including
mineral fuel, woven apparel, repaired
products, precious stones, and knit
apparel. US imports of agricultural
products from Oman were $2 million
(RO769,231).
<br>
            <br>
            However, trade agreements would
spur increased exports and stimulate
investment in a strategic, if not tiny,
regional market. To get a bigger slice of
the multibillion-dollar construction pie
in Lebanon, Qatar, Oman and the UAE
these days, the Association of
Equipment Manufacturers has been
clamouring for expanded trade agreements
in the Gulf. The group represents
Caterpillar, Gehl and Astec Industries,
among others.<br>
            <br>
US Trade Representative Robert B. Zoellick travelled to the UAE and the
Sultanate of Oman last year to discuss
efforts to promote economic growth and
opportunity by expanding bilateral,
regional and global trade. ‘‘In recent
years, the Gulf region has become associated
with economic reforms and trade
liberalisation, with the launching of the
current World Trade Organisation
(WTO) negotiations in Doha, Qatar, and
our recent completion of an FTA with
Bahrain. I’m interested in learning more
about the economic reforms and development
plans in Oman and the UAE,’’
Zoellick had then said.
<br>
            <br>
            ‘‘US FTAs are high quality, cutting edge
agreements that can play an important
role in stimulating economic growth
and development over time,’’ said
Zoellick. ‘‘Both Oman and the UAE have
expressed interest in deepening their
economic relationship with the US,
including with FTAs, and I look forward
to discussing the challenges and opportunities
involved in negotiating a comprehensive
FTA with the US.’’
<br>
            <br>
            In Oman, Zoellick met Minister of
Commerce &amp; Industry HE Maqbool Bin
Ali Sultan; and Minister of National
Economy HE Ahmed bin Abdulnabi
Macki. The US earlier concluded an FTA
with Bahrain, and signed a TIFA with
both the UAE and Oman. The TIFAs
provide a forum for the US to examine
ways to expand bilateral trade and
investment and can be used as a forum
to launch negotiations for an FTA.
<br>
            <br>
            HE Maqbool
Sultan confirmed last
month that 40 US
officials will be visiting
Oman this month
for negotiations to
conclude an FTA.
There will be 12
teams deliberating on
different topics from
labour to customs
issues, he said, noting
that Oman was prepared
to sign the FTA on a bilateral basis despite opposition
from Saudi Arabia.<br>
            <br>
According to the minister, further
negotiations will take place in March
and April for the FTA, which both
nations are expected to sign in July. A US
delegation will visit Oman on March 12-
14 to discuss the accord and an Omani
delegation will hold talks in the US on
April 18. If approved by the US
Congress, the agreement will become
effective in 2006, revealed the minister.
<br>
            <br>
            Including an investment chapter in
the US-Oman FTA is extremely important
not only for US
investors and US economic
interests, but also for Oman’s
own diversification priorities
which require significant foreign
capital. Early last
month, HE Maqbool Sultan
acquainted businessmen,
Omani private sector officials
and representatives of
the companies operating in
the Sultanate with the FTA
the Sultanate intends to sign
with the US.
<br>
            <br>
            The minister conformed
that the US had signed similar agreements
with Australia, Jordan, Morocco,
Bahrain and Singapore and is negotiating
for the same purpose at present with
the Sultanate and the UAE. He said a
framework agreement on investment
and trade between USA and AGCC
states had already been signed and the
Sultanate was the last state to ink the
pact in June last year.
<br>
            <br>
            He said that the agreement was the
first and would be followed by the signing
of the FTA with the US. HE
Maqbool Sultan cited two reasons for
the Sultanate’s signing the FTA individually
with the US. He said the first reason
was that the US sees that the agreement
should be concluded between
states and not with economic blocs like
the AGCC because in that case, it will
take years to conclude.
<br>
            <br>
            The second reason, the minister
said was that the pact would not cover
commodities only, but include services
and other matters governed by different
laws in different AGCC member
states like foreign banks and the attitude
towards telecommunications sector
liberalisation.
<br>
            <br>
            The minister reviewed the subjects
covered in the planned agreement which
include custom duties, financial services,
communications, insurance, capital market,
foreign capital share, government
purchases, tenders and labour.
<br>
            <br>
            Eng. Salim bin Said al Ghatami,
OCCI President, said in an address at the
outset of the meeting, the Sultanate seeks
by entering into negotiations with the
US to open up prospects for investment
cooperation and expanding trade
exchange and free flow of commodities
and services between the two countries.
<br>
