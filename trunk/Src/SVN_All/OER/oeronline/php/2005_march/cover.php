            <b>UAE: A deal in the making</b> <br>
            UAE was a founding member of the
World Trade Organisation (WTO) and
continues to participate on multilateral
trade liberalisation efforts. It has
worked to diversify its economy particularly
through tourism, manufacturing,
financial services and entrepot trade,
fostered by its development of the
largest man-made seaport in the world.
In March 2004, UAE signed a TIFA with
the US and, in December 2004, it also
signed a Container Port Security
Initiative with the US, to protect global
trade routes.
<br>
            <br>
            The UAE, home to the third-busiest
port in the world and a $8 billion
(RO3.08 billion) customer of US defence
weapons, wants its own deal completed
in Bush’s second term. In mid-January,
CMS Energy testified before officials at
the US trade representative’s office in
favour of a UAE trade agreement. Easy
to see why: The UAE’s soaring demand
for electric power bodes well for CMS,

which owns 40 per
cent of an $800 million
(RO307.7 million)
independent
water and power cogeneration
plant in
that Arab state.<br>
            <br>
US goods exports to the UAE in 2003
were $3.5 billion (RO.135 billion), including
machinery, aircraft, vehicles, electrical
machinery and optic and medical
instruments. US exports of agriculture
products to the UAE totalled $259 million
(RO100 million) in 2003, including
live animals and tree nuts. US goods
imports from the UAE in 2003 totalled
$1.1 billion (RO423 million), including
mineral fuel, woven apparel and knit
apparel.
<br>
            <br>
            With Bahrain as a precedence, the
successful conclusion of FTA talks
between the US and UAE is seen as a
given by most quarters within both the
countries. However, the UAE still has
some way to go to reach the required
economic standards. In particular, the
US has highlighted labour laws as a
cause for concern, stating that the UAE
and its neighbours will have to implement
International Labour
Organisation-consistent laws before any
agreement can be reached.
<br>
            <br>
            ‘‘UAE will have to change its labour
code to meet international labour laws,
revise the agency law and investment /
sponsorship rules in order to sign a FTA
with the US,’’ said Zoellick. In response,
the UAE has announced plans to allow
its mainly expatriate workforce to set up
trade unions, a key condition of any US
free trade pact.
<br>
            <br>
            <b>Trade beyond US
</b><br>
            The GCC and its primary trading partner,
the European Union (EU), are working
actively towards an FTA between the
two trade bodies. Both of them are looking
at segments of cooperation to boost
the member economies between the
regions. The GCC plans to sign the longawaited
pact between the GCC and the
EU in the near future.
<br>
            <br>
            Saudi Arabia took a prudent step late
last year in boosting inter-region trade,
especially as the biggest market in the
region. It removed double pricing on
gas, in return for the GCC agreement on
meeting a call on the EU to lift taxes on
petrochemicals and aluminium products.
The biggest stumbling point in the
talks has been trivial issues such as the
double pricing on gas and EU taxation
on Gulf petrochemicals and aluminium
between the two groups.
<br>
            <br>
            Moreover, in the last quarter of 2004,
the government of India kick-started the
process for clinching a FTA with the
GCC, with which New Delhi had
entered into a framework agreement for
economic cooperation in August. The
India-GCC joint commerce and trade
committee, which will meet defined the
contours of a preferential/FTA with the
GCC, which accounts for 11 per cent of
India’s exports.
<br>
            <br>
            The framework agreement with the GCC is aimed at exploring opportunities
in areas of oil and gas, knowledge-based
products, education, health and other
investments. Indo-GCC trade has lately
been on the rise. Indian exports to the
GCC countries were about $7 billion
(RO2.7 billion) in 2003-2004, a 43 per cent
jump over the previous year. Imports
(excluding oil) from the GCC
were $3.5 billion (RO1.34 billion)
last fiscal, which was 72
per cent higher than the
imports the year before.
<br>
            <br>
            The potential for regional
and bilateral cooperation is
very vast and the agreement
would enable India and the AGCC countries to work
together. The second AGCC India Industrial Conference will be held in Oman in 
            January 2006.</font></p>
