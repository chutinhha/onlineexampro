            <p align="justify"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">OMAN-US FTA
WHAT’S THE DEAL?
</font></p>
            <p align="justify"><b><font face="Verdana" size="1">This month, Oman will start
the first round of negotiations
with the US in a bid to sign a
free trade agreement (FTA).
What are the implications for
Omani business and economy
of such an agreement? What
are the learnings from the FTA
signed by Bahrain last year?
What, indeed, are FTAs and
RTAs and other forms of bilateral/
regional trade agreements
and how do they help the signing
parties? An OER insight</font></b></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im1.jpg" align="left" hspace="4" width="250" height="252">Till the Asian tsunami disrupted
the economic growth party at
the fag end of the year, 2004
was shaping up to be the
healthiest year for developing
countries in the last three decades. East
Asia had come out of the crisis of
1997–98 stronger and more vibrant than
ever; the countries of Europe and
Central Asia were almost completely out
of the long shadow of transition from
socialism and were growing more rapidly;
and South Asian countries, on the
strength of continuing reforms, were
performing well.
<br>
            <br>
            Moreover, countries in Latin
America, Sub-Saharan Africa,
and the Middle East and North
Africa had a much better year. To
be sure, some countries within
each region had not enjoyed the
fruits of this recovery, and these
countries remain a source of concern.
But in aggregate, with or
without the tsunami, economic
growth in 2004 was impressive.
<br>
            <br>
            World trade grew by 10.2 per
cent in 2004, and played an
important role in the year’s exemplary
performance. On the policy side, the WTO discussions in August 2004
recouped the ground that was lost in late
2003 (after the Cancun WTO ministerial),
when governments concluded an
agreement that provides a framework
for pursuing the Doha Development
Agenda. The framework, however, is
only an outline—the actual agreement is
still to come. Whether the final agreement
contains provisions that will provide
a meaningful impulse to development
remains to be seen.
<br>
            <br>
            In the meantime, OER examines the
evidence on an important development
that is reshaping the fabric of the world
trading system: The dramatic proliferation
of regional trade agreements
(RTAs). These take various forms of
preferential reciprocal treaties—they can
be bilateral or plurilateral free trade
agreements (FTAs) or, less commonly,
customs unions.
<br>
            <br>
            <b>The background
<br>
            </b>In May 2003, US President George W.
Bush announced his initiative to create a
Middle East Free Trade Area by 2013.
Bush’s Middle East Free Trade
Agreement initiative seeks to promote
free trade throughout the region and
between the region and the US. Keeping
in mind the differing levels of development
across the region, the US is taking
a graduated step-by-step approach to
creating a free trade area with countries
interested and willing to open their
economies and liberalise their trade
regimes. Bush had outlined a country by-
country plan to achieve that goal.
<br>
            <br>
            Since that announcement, the US has
concluded FTA negotiations with
Morocco and Bahrain, signed Trade and
Investment Framework Agreements
(TIFAs) with Saudi Arabia, the United
Arab Emirates (UAE), Kuwait, Qatar
and Yemen, and now with Oman.
Recently, the 9/11 Commission has
urged the US to expand trade with the
Middle East and highlighted the recently
concluded FTA with Bahrain, as well
as the US’ recently passed FTA with
Morocco as examples of positive steps in
this direction.
<br>
            <br>
            The US already has TIFAs with
Egypt, Algeria, and Tunisia. The US held
its first TIFA Council meetings with the
UAE, Kuwait, Qatar, and Oman last year. In addition, the US has made progress with the WTO accessions of
Saudi Arabia and Algeria, and in the
case of Algeria, extended GSP benefits.
<br>
            <br>
            The US is working to open markets
globally in the Doha WTO negotiations;
regionally through APEC and the Free
Trade Area of the Americas (FTAA)
negotiations; and bilaterally, with
FTAs. The Bush Administration has
completed FTAs with eleven countries
—Chile, Singapore, Costa Rica, the
Dominican Republic, El Salvador,
Guatemala, Honduras, Nicaragua,
Australia, Morocco, and now Bahrain—
in the last two years.<br>
            <br>
Negotiations are under way or about
to begin with 10 more countries: Panama,
Colombia, Peru, Ecuador, Thailand,
and the five nations of the Southern
African Customs Union (SACU).
New and pending FTA partners, taken
together, would constitute America’s
third largest export market and the sixth
largest economy in the world.
<br>
            <br>
            While the media and the public focus
on the deadly turmoil in Iraq, business
lobbyists have turned their attention to
another side of the fragile Middle East
region. Following up on the Bush
Administration’s call for a Middle East
Free Trade Area that would ‘‘embrace
and encourage reformers across the
region,’’ these businesses want Bush to
sign and implement FTAs with Oman,
the UAE and Bahrain as one of his first
post-inauguration tasks.
<br>
            <br>
            “Free Trade
Agreements with
Oman and the UAE
will help advance market-
oriented economic
reforms and will
help to demonstrate
to other Middle
Eastern nations the
important benefits of
trade liberalisation,”
said Bill Reinsch, President
of the National
Foreign Trade Council. “The
US-Middle East Free Trade
Coalition will work hard to
ensure these negotiations are
successful and that both
FTAs become a reality.”
<br>
            <br>
            Aerospace giant Boeing;
oil and chemical heavyweights
Dow Chemical, ChevronTexaco and Exxon
Mobil; and technology firms
Motorola and Intel are among those corporations
that have been urging congressional
approval of a Bahrain FTA,
signed in 2004. They also want to see
deals signed with Oman and the UAE.
(Exxon and Chevron have both donated
$250,000 (RO96154) to the 2005 presidential
Inaugural Committee; Boeing gave
$100,000 (RO38462).)
<br>
            <br>
            And just days before the inauguration,
the powerful and well-connected
Pharmaceutical Researchers and
Manufacturers of America (PhRMA)
weighed in on the issue too, saying a
trade agreement with Oman would provide
life-saving medicines to the region
and protect intellectual property standards
that make new drug R&amp;D possible.
Susan Kling Finston, PhRMA’s associate
vice-president for Middle
East/North African affairs, adds, ‘‘The
standards will promote an attractive
environment for foreign investment.’’
<br>
            <br>
            On the other hand, Qatar has held
two consultative meetings recently to set
out a negotiation schedule for a FTA
with the US. Economy Minister Sheikh
Mohammed bin Ahmad Al Thani said
last month the Americans asked to negotiate
separately with each Gulf state, and
that negotiations with Qatar would
begin later this year. Kuwait’s Trade and
Industry Minister Abdullah Al-Taweel
and US Trade Representative Robert
Zoellick signed a TIFA last month. This
accord has been described by Kuwait’s
Ambassador to the US Salem Al-Sabah
as ‘‘an introduction to the first round of
            FTA negotiations that will serve the
interests of the peoples and economies of
our two countries.’’
<br>
            <br>
            Subsequent to the TIFA’s signing,
the creation of a Kuwait-US Council for
Trade and Investment is expected to be
finalised. Ambassador Al-Sabah said the
Council, operating within the framework
of reaching a free trade agreement,
will review Kuwait-US trade and investment
relations as a step toward broader
commercial and economic opportunities.
In 2003, the US was Kuwait’s largest
single supplier of imports. That year,
Kuwait’s total imports were close to $7.5
billion (RO2.88 billion).
<br>
            <br>
            <b>FTA = RIFT?</b> <br>
So who would object to more Middle
East trade deals? None other than Saudi
Arabia, where most of the world’s oil
reserves sit, gave Bahrain the cold shoulder
when it signed a FTA with the US in
September 2004 (it won’t be implemented
until Congress approves it). Two-way
trade amounted to $887 million
(RO341.15 million) in 2003, with 57 per
cent of the total representing exports to
Bahrain, the unofficial financial hub of
the Gulf region. The trade agreement
would allow 100 per cent of US consumer
and industrial exports and 81 per
cent of farm produce to enter Bahrain
duty-free.
<br>
            <br>
            The Saudis contend that free trade
deals with the US could
undermine efforts to
promote greater regional
cooperation, since
the six-member Gulf
Cooperation Council’s
(GCC) rules state that
all imports from outside
the group should
incur a 5 per cent import
tariff. Saudi Foreign
Minister Prince
Saud al-Faisal has
publicly expressed his
worries that when
GCC members sign bilateral deals separately
with international powers, the
required teamwork spirit is violated.
<br>
            <br>
            Economists feel that the Saudi-
Bahraini dispute over Manama’s free
trade pact with Washington that overshadowed
the latest summit of Gulf
Arab states could derail the Gulf
Cooperation Council’s (GCC) bid for
economic integration. GCC leaders
wound up the annual Zayed (25th GCC)
Summit in the Bahraini capital Manama
in December last year without settling
the row, casting strong doubts on
whether the six-member bloc will be
able to implement economic pacts.
<br>
            <br>
            Contrary to previous summits, the
final communiqué of the Manama meeting
dropped any reference to GCC plans
for a monetary union next year, a common
market in 2007 and a single currency
by the beginning of 2010. It also failed
to state an intention on the part of member
states to clear obstacles hindering a
smooth implementation of a Gulf customs
union launched two years ago.
<br>
            <br>
            Before launching the customs union
on January 1, 2003, the GCC states
agreed to delay the implementation of
some parts for up to three years in a bid
to find solutions to a number of political
and logistical problems. Under the
union, member states are supposed to
become a single customs zone in which
the duty on foreign
imports is 5 per cent.
All member states
have fixed their customs
tariffs at the
same level.
<br>
            <br>
            The GCC states,
however, have been
unable to find permanent
solutions for the
functioning of joint
customs centres set up
to facilitate the movement
of goods. They
also failed to overcome
problems created by applying customs
protection to scores of national
products, with each member state submitting
a list of goods to be excluded
from the union.
<br>
            <br>
            All six GCC member states have
agreed to share customs revenues temporarily
on the basis of the final destination
of imports, but have so far failed to
reach a permanent formula. They have
also prepared a draft law for anti-dumping,
but it has not been ratified. And they
are seeking to resolve conflicting interests
among agents of similar products in
various member states.
<br>
            <br>
            “GCC economies are competing
            with, rather than complementing, each
other. This has reduced inter-Gulf trade
to less than 10 per cent of the size of their
combined trade,” Abdulwahab Al
Harun, head of the Kuwaiti parliament’s
finance panel, told a symposium early
December last year.
<br>
            <br>
            To achieve a viable monetary union,
the Gulf states must implement key criteria
to bring their economic and fiscal
policies closer. They have approved in
principle standard criteria like budget
deficit, ratio of public debt to GDP, inflation
and interest rates and others, but no
permanent deals have been struck.<br>
            <br>
The precise parameters of the criteria
have not been finalised although they
agreed to set up a Gulf central bank. The
present ratios of public debt to GDP in
GCC states range between 17 and 80 per
cent while the maximum allowed percentage
in the European Union, for
example, is 60 per cent.
<br>
            <br>
            However, according to Bahraini
Deputy Premier and Foreign Minister Shaikh Mohammed bin Mubarak Al
Khalifa, all the five other GCC member
states (except for Saudi Arabia) are negotiating
their own FTAs with the US. He
said while the issue of bilateral trade
agreements involving member states
was reviewed at the 25th GCC Summit,
further discussions to resolve differences
of opinion related to the negotiations
were postponed until GCC leaders
decide to continue.<br>
            <br>
‘‘All GCC countries agree on the
benefits of bilateral FTAs and no country
would sign any agreement if it was
harmful to another,’’ he had said. He
dismissed as untrue reports that Saudi
Arabia intended to impose duties,
because the FTA agreement signed by
Bahrain and similar deals being negotiated by other member
states, were not
in line with the GCC
Customs union.
‘‘This is far from the
truth. What country
would impose restrictions? All GCC
countries are taking measures to
reduce Customs obstacles at entry
points,’’ he said.
<br>
            <br>
            He also dismissed as untrue reports
that Saudi Crown Prince Abdullah had
not attended the summit in view of the
differences of opinion related to the FTA. ‘‘Crown Prince Abdullah issued a
statement in which he wished the summit
every success and Prince Sultan is an
outstanding representative for Saudi
Arabia at the summit.’’ He said that the
FTA was not an issue exclusive to
Bahrain and that all the other GCC states
were at various stages of negotiations.
<br>
            <br>
            He said that all member states
agreed to postpone discussion on the
progress of negotiations. ‘‘In fact, discussions
were not included in the summit
agenda because they were raised
one week before it was held,’’ he said.
‘‘Our main objective is to coordinate
our efforts, which all member states
agree is the best solution.’’ Shaikh
Mohammed said that all GCC countries
were negotiating an FTA with the US
individually ‘‘because the US could not
carry out the agreement with the GCC
states as a group’’.
<br>
            <br>
            He said that talks between the GCC
states and the European Union on economic
co-operation and a free trade area
would continue. ‘‘However, talks with
the EU have taken a long time, more
than 15 years. We hope that in a meeting
with GCC foreign ministers with their
EU counterparts in April, these issues
will be resolved,’’ he said.
<br>
            <br>
            He dismissed as untrue reports that
disputes at the summit had hindered
other economic agreements. ‘‘Yes, talks
on bilateral agreements were postponed,
but all other economic issues were
agreed on,’’ he said. ‘‘The final communiqué
included 11 economic-related
agreements, proposals and directives.’’
<br>
            <br>
            <b>US-Bahrain FTA: Dissecting the
deal
</b><br>
            The economic importance of the
Bahrain-US Free Trade Agreement
(FTA) and its potential impact on the
economy and the private sector cannot
be overstated. If implemented successfully,
the FTA will help in the exchange
of trade and services, not only between
Bahrain and the US, but also make the
kingdom an attractive centre for foreign
investments and industries that wish to
penetrate the US market.
<br>
            <br>
            The FTA is aimed at liberalising
trade in goods and services, facilitating
free market competition, stimulating the
inward flow of investment and expansion
in key manufacturing and services
sectors, encouraging the exchange of
expertise, and creating employment
opportunities. The FTA is also aimed at
making Bahrain a transit gateway for
American products to go to other countries
in the region. The Bahrain Chamber
of Commerce &amp; Industry (BCCI) is a
member of the FTA Supreme Committee
and has nominated its representatives in
the five emanating sub-committees,
namely: services, industrial, textile, legislative
and promotion subcommittees.
<br>
            <br>
            Early stages of the
Bahrain-US free trade negotiations
go back to the year
1999, with the signing of the
Bilateral Investment Treaty
(BIT), which entered into
force on May 30, 2001. It was
the first such treaty signed
between the US and a member
of the GCC and aimed at
stimulating the flow of private
investment between the
two countries. One year
later, the Trade &amp; Investment Framework Agreement
(TIFA) was signed on June 18,
2002, representing the prelude
for the FTA negotiations. The
TIFA was designed as a forum
for an ongoing bilateral dialogue
on economic reform and
trade liberalisation. Negotiating
an FTA with the US constituted
the next step.
<br>
            <br>
            The Kingdom of Bahrain and the
United States announced their intention
to negotiate an FTA on May 21, 2003.
HH the Crown Prince Sheikh Salman
Bin Hamad Al-Khalifa and Ambassador
Robert Zoellick, United States Trade
Representative, then discussed the
preparatory steps for the launch of Free
Trade Negotiations. On August 4, 2003,
Ambassador Zoellick formally notified
the US Congress of the Administration’s
intent to initiate FTA negotiations with
Bahrain.
<br>
            <br>
            After two rounds of FTA negotiations
on January 26, 2004 in Manama,
and during March 1–5, 2004 in
Washington, D.C., and an interim round
of technical discussions in London during
April 19–22, 2004, Bahrain’s Minister
of Finance &amp; National Economy, HE
Abdulla Hassan Saif, and Ambassador
Zoellick announced in Washington DC
the successful conclusion of the negotiations
on May 27, 2004.
<br>
            <br>
            Minister of Finance and National
Economy, HE Abdulla Hassan Saif led
the government of Bahrain’s negotiation
team while the US team was led by
Assistant United States Trade Representative
for Europe and the Mediterranean,
Ms. Catherine Novelli. Specific
chapters of the Free Trade Agreement
were negotiated by individual negotiation
committees, which comprised of
representatives of all the ministries and
government agencies concerned.<br>
            <br>
