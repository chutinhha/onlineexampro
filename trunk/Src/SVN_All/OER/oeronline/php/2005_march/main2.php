pornographic results returned, by search engine<br>
Source: ‘How Internet filters affect the search for online health information’ 
study by Kaiser Family Foundation, 2002</i><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
<b>Staggering statistics</b><br>
While adults have every right to view explicit material of their choice, the 
number of children using the Internet is growing rapidly, and is expected to 
exceed 20 million by the year 2002, and we ought to be concerned. Children are 
prone to soaking up new technologies faster than adults. Nearly 44 per cent of 
teenagers have visited a sexually explicit Website according to Time Magazine, 
2000. ‘‘Nearly a third (31 per cent) of kids aged 10-17 from households with 
computers (24 per cent of all kids aged 10-17, irrespective of whether they have 
a PC in their household or not,) say they have seen a pornographic Web site 
(Source: NPR Online at <br>
http://www.npr.org/programs/specials/poll/technology). Even legitimate search 
for health-related words lead to porn sites as shown in the figure below:<br>
<br>
Internet laws concerning pornography or child obscenity are jurisdictional, 
which in turn depend on local cultures. Hence, the definition of what is to be 
considered obscene or how child pornography is to be classified are all blur. 
The porn industry, which is highly networked and in favour of freedom of speech, 
has, in part, made countries such as the US and Australia become tolerant of 
pornography. <br>
<br>
Again, when profit margins of online stock-trading is about 0.2 per cent, the 
porn sites’ profit margin is 30 per cent, giving a strong business case 
(Christianity Today, 2000). Pornography Web sites earned $1.5 billion in 1999 
and more than $2.0 billion in 2000. The Industry Standard reports that 70 per 
cent of porn traffic occurs between 9 and 5, and people in the Eastern Time zone 
account for the largest number of porn-site hits - 30.3 per cent.<br>
<br>
The National Research Council’s Computer Science and Telecommunications Board 
explores a number of fundamental questions that are useful to be quoted here: 
Who defines what is inappropriate material? Do we control Internet access by a 
17-year-old in the same manner as for a 7-year-old? What is the role of 
technology and policy in solving such a problem in the context of family, 
community, and society? An attempt to answer these questions is made by this 
study. <br>
<br>
<b>Obscenity defined</b><br>
Materials that require protection under freedom of speech need clear 
demarcations from obscene material. This is essential in any court of law before 
prosecution of the accused. There exist differences of opinion among countries 
about transmission of child pornography. <br>
<br>
In Roth Vs. United States (354 US 476, 1957) case, Roth was convicted for sale 
of obscene books, photographs and magazines. The federal court made clear that 
the first amendment of the US constitution gives protection to speech and press 
for all ideas having even the slightest redeeming social importance. But 
implicit in the history of the First Amendment is the rejection of obscenity as 
utterly without redeeming social importance.<br>
<br>
Every state reserves their legitimate interest in prohibiting dissemination or 
exhibition of obscene material when the mode of transmission carries with it a 
significant danger of offending the sensibilities of unwilling participants or 
the exposure to juveniles. <br>
<br>
<b>Miller Case</b><br>
In the case Miller Vs California (413 US 15, 1973), Marvin Miller was under 
trial for distribution of unsolicited obscene material by mail. In accordance 
with California Penal Code section 311.2, he was found guilty of misdemeanour. 
According to this section, obscene means ‘that to an average person, applying 
contemporary standards, the predominant appeal of the matter, taken as a whole, 
is to prurient interest, that is, a shameful or morbid interest in nudity, sex, 
excretion, which goes beyond customary limits of openness in description or 
representation of such matters and is matter which is utterly without redeeming 
social importance’.<br>
<br>
The final verdict of conviction recommends as guidelines the three main tests of 
obscenity:<br>
• Whether the average person, applying contemporary community standards would 
find the work, taken as a whole, appeals to the prurient interest;<br>
•Whether the work depicts or describes, in a patently offensive way, sexual 
conduct specifically defined by the state of law;<br>
•Whether the work, taken as a whole, lacks serious literary, artistic, 
political, or scientific value;<br>
Speaking ethically about the above guidelines, some the following questions are 
certainly thought provoking:<br>
1) What are the criteria for an average person to evaluate obscenity?<br>
2) Contemporary standards of the community are variable – so, which one applies 
on the Internet?<br>
3) Can there be a yardstick for measuring prurient interest in the subject 
matter as having tendency to excite lustful thoughts?<br>
4) Obscene material online transmission is fixed at source or at destination for 
legal jurisdiction?<br>
5) ‘Patently’ and ’offensive way’ are subjective issues. Aren’t they?<br>
6) Aren’t simulations with virtual children for pornography prosecutable?<br>
7) Is it not possible to define obscenity in a constitutional manner for the 
ease of applying legislation?<br>
8) Can connectivity service providers, chat-room hosts, bulletin board operators 
be liable for trafficking obscene material across any jurisdiction?<br>
9) When adult customers request for obscene material, can they be prosecuted 
too? <br>
10) Will the viewing of obscene material at one’s home be treated in a same 
manner by law?<br>
<br>
<b>Challenge of jurisdiction</b><br>
Does receipt of unsolicited obscene material across borders penalise the sender 
in all those countries? Consider this US based case with the accused in 
California being penalised under Tennessee laws. Robert Thomas and Carleen 
Thomas, operators of Amateur Action Computer Bulletin Board System from 
California, were found guilty in Tennessee for enabling exchange of obscene 
material in digital format to approved members of its service. <br>
<br>
Agent David Dirmeyer produced sufficient evidence in front of the law, acting as 
a bulletin board member in an assumed name from Tennessee, having business deals 
with Thomas for adult content. Thus, in interstate commerce, it is indeed 
possible to prove jurisdiction at every transmission reception point. Venue in 
this case was established by preponderance of evidence at Tennessee and 
continues to indicate that it could be any place in which this bulletin board 
could access and transact further business. Many national laws protect their 
nationals from such illicit content receptions and attacks with local 
jurisdictional venue.<br>
<br>
<b>Content providers differentiated with network service providers</b><br>
Any creation, sale or distribution, transportation of obscene material requires 
prosecution. Use of interactive computer services for these activities in both 
interstate and international commerce must be treated in a similar fashion. 
Persons responsible for transmission of obscene materials to minors must be 
legally sued while such email transaction between two friends or lovers with 
mutual consent should not be.<br>
<br>
It is critical to prosecute the content creator or originator in case of obscene 
material in transit rather than penalising the service provider who offers 
network connectivity for this activity. Again the service provider deserves 
defence if he has acted in good faith in preventing minors from accessing the 
prohibited material. This can be plausible by requiring credit card numbers or 
social identities, which in turn are linked to age eligibility criteria for 
censorship. <br>
<br>
The online service or computer interactive service provider entitles protection 
from liability if he has offered for blocking and screening offensive material 
provided by any other content provider. Any such offensive material when deemed 
by the court to be of social value could also be protected against criminal 
liability. Such provision are given due considerations under the Communications 
Decency Act (CDA) 1996 of US. The Child Pornography Prevention Act (CPPA) goes 
ahead to criminalise the use of computer technology to knowingly produce child 
pornography even as virtual images children.<br>
<br>
In Germany, a CompuServe (network connectivity provider) General Manager Felic 
Somm was charged for permitting the spread of child pornography posted to Usenet 
newsgroups (like online notice boards) by anonymous CompuServe members. It could 
even be the case that Somm was not aware of these contents or it was technically 
impossible to filter the content before dispersion. <br>
<br>
However Avinish Bajaj, CEO of Bazee.com was arrested (and subsequently released 
on bail) recently for failing to check the sale of the pornographic VCDs through 
his portal under Section 67 of the IT Act of India. The alleged video clipping 
was not hosted on the portal but its sale took place through this portal through 
e-mail communications. This case even advocated changes of this Act to protect 
service providers who have been diligent in warding off pornography from their 
portals. <br>
<br>
<b>Liabilities of employers</b><br>
In the event that an employee uses his office computer and/or network services 
to create, disseminate or transmit obscene material, the employer will not be 
liable for legal action in case he has taken sufficient precaution to dissuade 
these activities. It is also essential for employers to have strict policies and 
guidelines in the matter of use of office equipment in order gain protection 
from vicarious liability. <br>
<br>
Adherence to internal /industry-based code of conduct also shifts liability in 
the case of employers. In addition, the employer must not disregard misconduct 
and take corrective action as per their policies or else it is considered that 
the employer also ratifies such misconduct. The scope of employment also defines 
the scope within which the liability can be shifted on to the employer by any 
action involving creation or dissemination of obscene information, performed by 
the employee. All this reinforces the ethical and moral responsibility on the 
part of the employees themselves.<br>
<br>
<b>Technical means of censorship</b><br>
At server level, software can be installed to filter Website search results that 
include obscene sites according to local customs and standards of decency. 
Filtering and blocking software, sometimes called ‘filterware’, may be solicited 
by service providers as a means of self-regulation with social motives. By 
implementing this safeguard, Omantel, the local service provider for Oman, 
provides an important service to adults, especially parents. <br>
<br>
Even ethically speaking, adult content providers can classify their contents and 
offer these only based in PIN or password activation. The control at these 
points can be planted based on age eligibility. Internet content hosts require a 
periodic scan of material hosted on their servers while any violations to their 
agreements must be treated with immediate effect and severity. <br>
<br>
Content scrambling such as those implemented for television signals of 
paid-channels is another technical means for protection while keys to unscramble 
need effective channels of distribution under control. Simple awareness among 
public about the technical possibility of checking for transmitted data for 
obscenity will ensure an element of control in dissemination. Society plays the 
major role with peer-pressure on morals of conduct supplemented by technology.<br>
<br>
<b>Further issues to be addressed</b><br>
Obscenity must not be limited to pornographic material but also apply stringent 
limitations to acts of extreme violence and advocacy of drug abuse. The extent 
of technology in creating virtual images has advanced sufficiently to mislead 
even animated child characters to be perceived with the look and feel of real 
children. Hence virtual images in the case of child pornography must be treated 
at par with real images and hence strictly banned under law. Mankind can 
restrain their pervertedness to superimposing adult acts on adult images. <br>
<br>
Even adults who may be corrupted or depraved by receiving obscene material 
deserve protection. This issue is addressed in the definition of obscenity 
according to the UK Obscene Publications Act of 1959. Henceforth, it is a matter 
of prerogative for adults to seek protection from such unsolicited obscene 
transmissions.<br>
<br>
Misuse of email communication as in the case of a Delhi business graduate who 
distributed pornographic material in obscene language through emails in the name 
of his wife to her friends to harass her must lead to legal prosecution. Illicit 
porn traffic can also amount to defamation as it tarnishes the reputation of the 
unsolicited receiver. Email registrations need no identity proofs at the time of 
their creation. It certainly is call of the current times especially when email 
communications are considered legally valid. Email service providers must enable 
blocking of such obscene emails when requested by their subscribers explicitly.
<br>
<br>
<b>Oman’s telecom regulations:</b><br>
The Apex body governing the telecom industry of Oman, Telecommunications 
Regulatory Authority (TRA – www.tra.gov.om) shall base its control of the issues 
discussed above, on the Royal Decree No. 30, promulgated in 2002. Article 61 of 
this Act, punishes people who send (or even forward) by means of 
telecommunication systems (inclusive of any system involving the conveyance of 
signals or text or sound or visual images by wire or radio or optical or other 
electromagnetic means) a message that violates public order or public morals, or 
is not true and he is ware of it, or aims to disturb others.<br>
&nbsp;<br>
The legal penalty for this offence can be a 1-year imprisonment and/or RO1000 
fine, which doubles in case of repeated offence. The law certainly addresses 
misuse of telephone, mobile phones, Internet (e-mails and chats), wireless and 
infrared transmissions, radio and television communications in a comprehensive 
manner albeit elucidating the manner of producing evidence of abuse or 
addressing the case of stolen identities.<br>
<br>
<b>Final word</b><br>
The Internet promotes the freedom of speech of expression for netizens to voice 
their opinions freely. However, use of this medium for harassing or defaming or 
abusing through electronic communications must be legally deterred. Lawmakers of 
today must base legal premises on a firm framework that channelises this medium 
rather than suppressing its potential on grounds of misuse. <br>
<br>
Filtering using filterware may advocate social motives. Even the electronic news 
media plays a unique role in keeping the public informed and disseminating 
information freely without restriction while balancing on a sword’s edge when it 
comes to obscenity online. They in turn could opt for self-rating of content or 
follow the cyber-compliance system initiated by industry leaders.<br>
<br>
Again, irresponsible use of electronic gadgets to covertly capture images, 
exceeding decency as well as privacy limits must be curbed and impeached legally 
as proposed by the Video Voyeurism Bill 2004, US (yet to be enacted as law).<br>
<br>
E-Commerce must operate under activities that are not only morally sound, but 
also legally justifiable. It may attract the wrath of law, both civil as well as 
criminal, anywhere if anything concerning it is immoral or illegal. Any country 
intending to harness the power of ICT to improve its social and economic status 
in the global arena must face these cyber challenges in a prompt and decisive 
manner as an art of perfect balance between freedom of expression and handling 
of obscenity.<br>
