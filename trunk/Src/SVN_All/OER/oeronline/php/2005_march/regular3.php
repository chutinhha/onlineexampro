<p align="justify"><font face="Verdana" size="1"><b>GE Healthcare launches Vivid 
i at Arab Health 2005</b><br>
<img border="0" src="im20.jpg" align="left" hspace="5" width="125" height="117">GE 
Healthcare, a unit of General Electric Company, showcased the Vivid i—the 
world’s first miniaturised cardiovascular ultrasound system—at Arab Health 2005. 
Vivid i provides high-performance, full-featured imaging in a lightweight 
design. <br>
<br>
The Vivid i is the world’s first miniaturised cardiovascular ultrasound system, 
the result of integrating more than 20 years of expertise in ultrasound and 
cutting-edge computing technologies, as well as new patented technology 
developed by GE researchers. The Vivid i offers the functionality and high 
performance of full-featured, larger-scale systems – but in a portable and 
wireless design that weighs 30 times less. <br>
