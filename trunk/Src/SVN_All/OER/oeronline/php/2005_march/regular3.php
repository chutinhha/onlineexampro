<b>Intel produces chip with 2 ‘brains’</b><br>
Intel, the world’s largest microchip maker, has announced that it has completed 
the initial production runs of a chip with two processors-in-one, setting the 
stage for mainstream marketing of personal computers with two processor 
‘brains.’ Intel will add two so-called dual-core products to its Pentium family 
of chips during Q2, including the Pentium processor Extreme Edition, said a 
statement. <br>
<br>
Intel’s Pentium Extreme Edition will also include hyper-threading technology – a 
separate technology Intel began offering three years ago. With hyper-threading 
and dual cores, Intel chips can process four software calculations 
simultaneously by efficiently using resources that otherwise would sit idle. 
Intel’s announcement comes after the consortium of Sony, IBM and Toshiba 
announced details of their ‘Cell’ processor based on similar so-called 
‘multi-core’ technology.</font></p>

