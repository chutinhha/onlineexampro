<b>LG set to harness mobile handset growth</b><br>
<img border="0" src="im18.jpg" align="right" hspace="5" vspace="2" width="127" height="63">The regional mobile phone industry is growing rapidly and is set to increase on 
an average of 40 per cent annually. Leading industry analyst Gartner predicts 
that nearly 70 per cent of all mobile phones sold will have a built-in camera by 
2008. With the unveiling of its new flagship camera phones, LG is poised to 
harness this growth, consolidating its reputation as the innovation leader in 
handset design and functionality.<br>
<br>
To meet market demand, LG’s new LG T5100 is a dual screen, rotating clamshell 
style phone featuring an integrated 1.3 mega-pixel camera with flash 
functionality and video capability. Compact in design, the T5100 boasts a large 
262,000 colour internal screen and a 65,536 colour external screen. The 
integrated MP3 player allows music files, stored on the phone’s internal memory, 
to be played through the phone’s loudspeaker, or an attached stereo headset. <br>
<br>
Other phones launched in the region include the LG L1100, a tri-band camera 
phone suitable for global roaming with an attractive clamshell design. It is 
also GPRS and WAP enabled with integrated VGA quality camera with five picture 
resolutions, MMS multimedia messaging, voice memo recorder (for records up to 90 
seconds in 3 x 30 second recordings), Java engine and voice control. LG is 
targeting sales of $300 million (RO115.38 million) in handsets in the Middle 
East and Africa region this year.</font></p>

