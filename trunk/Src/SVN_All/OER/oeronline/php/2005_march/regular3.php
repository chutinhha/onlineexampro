<b>Sony Ericsson introduces Walkman phones</b><br>
Sony Ericsson’s new 2005 mobile music strategy announced last month at the 3GSM 
World Congress, Cannes, France, features the integration of high quality digital 
music players into its classic, stylish mobile phones under the world famous 
Walkman brand. <br>
<br>
Sony Ericsson’s product team felt that a credible mobile music player had to 
combine key features like easy-to-use software to copy music to the device, 
large music storage <i>
<img border="0" src="im19.jpg" align="left" width="100" height="208"></i>capacity, long battery life, quality headphones and cutting 
edge design. The company is ideally positioned to integrate these features with 
advanced phone functions including excellent voice, messaging, imaging, and 
on-line connectivity, to create a device that not only satisfies consumer needs 
but offers great opportunities for network operators to develop new and exciting 
services. <br>
<br>
The first Walkman branded Sony Ericsson music phones will be announced in March 
and will enable users to transfer their existing CD collections to their phones 
via a PC. Future products will be capable of playing copy-protected music files 
purchased and downloaded via a PC from the Internet or direct to the phone.<br>
<br>
<i>A demonstrator from Sony Ericsson shows the new 3G phone Z800i during the 3GSM 
