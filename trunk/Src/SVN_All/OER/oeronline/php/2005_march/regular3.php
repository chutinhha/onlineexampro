including bedside, as opposed being transported to an imaging lab in a hospital. 
GE’s engineers developed Vivid i by miniaturising the components of a premium 
echocardiography system weighing more than 400 pounds (180 kg), to provide a 
portable system weighing only 10 pounds (less than 5 kg).</font></p>

