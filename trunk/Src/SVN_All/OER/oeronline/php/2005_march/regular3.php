folding mobile phones with the stylish Nokia 6101 camera phone. In addition to 
offering consumers the latest mobile technology in an elegant fold concept, the 
Nokia 6101 provides operators with an extended range of customisation features. 
The compact, tri-band phone will be available in two versions: a GSM 
900/1800/1900 version primarily for Europe, Middle East, Africa and Asian 
markets, and a GSM 850/1800/1900 model targeted towards the Americas. With an 
estimated retail price of less than 250 EUR (RO123.50), excluding taxes and 
subsidies, the Nokia 6101 is expected to start shipping in the second quarter of 
2005.<br>
<br>
Nokia also introduced a new arrival to the 3G arena – the Nokia 6680 imaging 
smartphone at the same forum last month. The 6680 is optimised for visual 
sharing, boasting two integrated cameras, a flash, and a bright screen of up to 
262,144 colours. Designed for ease-of-use, the active slide covering the back 
camera of the Nokia 6680 provides fast and intuitive camera activation as well 
as effortless switching between cameras during a video call.<br>
<br>
3G-enabled features such as two-way video calls, video sharing and always-on 
e-mail, making the well-designed Nokia 6680 a versatile work tool, complement a 
wide range of smartphone features, including organiser, video streaming and 
Internet browser. Based on the leading smartphone Series 60 Platform, the 
quad-band phone for GSM 900/1800/1900, EDGE and WCDMA networks offers seamless 
roaming in over 100 countries around the world. The Nokia 6680 imaging 
smartphone is expected to be available this month onwards with an estimated, 
unsubsidised sales price of approximately 500 euros (RO247).</font></p>

