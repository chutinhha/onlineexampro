<b>And now, e-Parking!</b><br>
Cyber Gear has developed an online application that allows people to reserve a 
parking space using the Internet. According to Sharad Agarwal, CEO of Cyber 
Gear, ‘‘We built the application after studying the traffic and parking related 
problems of a growing city such as Dubai. The e-Parking engine allows the 
management and owners of car parks to configure the parking lot on their 
Website. Visitors to shopping malls, exhibition and event venues can view the 
available slots and reserve online. There is even a credit card-based payment 
gateway to receive online payments. We see this application as a necessary tool 
for government and private sector organisations to deploy in order to provide 
better service to their clients.’’</font></p>

