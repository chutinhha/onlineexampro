<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Contract 
services in the Sultanate</font></font><font face="Verdana" size="1"><b><br>
<br>
Several leading companies in the Sultanate have developed the necessary 
expertise to offer services in the areas of catering, cleaning, laundry, 
accommodation, operations and maintenance, and related areas as well, such as 
waste disposal, transportation, landscaping, gardening and pest control. Besides 
the key oil and gas industry, the other segments served by institutional 
contractors are education, healthcare, airports, corporate catering, company 
cafeterias, university campuses, labour camps, and clubs, among others. <br>
