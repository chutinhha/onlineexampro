<br>
<b>Renaissance Services SAOG<br>
Contract Services Group</b><br>
• Tawoos Industrial Services Company LLC<br>
• Rusail Catering and Cleaning Services LLC<br>
• Renaissance Services PAC Division <br>
• Renaissance Services Overseas Division<br>
<br>
<img border="0" src="im11.jpg" align="left" hspace="5" width="199" height="130">Among 
the biggest in the contract services trade is the Contract Services Group (CSG) 
of Renaissance Services SAOG. Established in 1986, CSG comprises four companies: 
Tawoos Industrial Services Company LLC (TISCO); Rusail Catering and Cleaning 
Services LLC (RCCS), a 100 per cent subsidiary of TISCO acquired in 1990, 
Renaissance Services PAC Division (RS PAC); and Renaissance Services Overseas 
Division (RS OD).<br>
<br>
CSG employs over 2,800 people (over 2,000 in its Sultanate of Oman operations 
and approximately 800 people in major international project in the Upper Gulf 
region). CSG activities include contract catering, cleaning, laundry, 
accommodation, operations and maintenance (O&amp;M), leisure, property, estate 
services and facilities management. CSG serves a wide range of customers 
including university students, schoolchildren, hospital staff, visitors and 
patients, military personnel and large-scale workforces, often in remote and 
challenging locations. The company also provides popular outside catering and 
function services.<br>
<br>
CSG operations are located throughout Oman and the Upper Gulf and the client 
base includes the oil and gas, mining, civil engineering industries, banks, 
property companies, public and private hospitals, hotels, recreation clubs, 
leisure operators, public authorities and institutions (particularly in the 
health and education sectors) and defence authorities. <br>
The company has invested in Build, Own and Operate projects for PDO to develop 
Permanent Accommodation for Contractors (PAC) at interior oilfield; Fahud, Nimr 
and Qarn Alam. The facilities include every amenity for messing, accommodation, 
recreational, and medical care. The PACs replace multiple contractor-owned 
temporary portacabin camps, and deliver significantly higher living and 
occupational health standards at reduced costs, achieved through long-term 
economies of scale. <br>
<br>
The company is also involved in Rapid Development life support projects in 
hostile/challenging environments demonstrating world-class capabilities in 
Design, Erect and Commission of Kitchen/Diner (DFAC) facilities with provision 
of Catering Services to military personnel through RSOD.<br>
<br>
The company’s reputation is based on great customer service, internationally 
competitive pricing through efficient operations, and outstanding HSE (Health, 
Safety &amp; Environment), ISO 9001:2000 Quality System and meaningful Omanisation 
track record.<br>
<br>
<b>AA Group of Companies</b><br>
<img border="0" src="im12.jpg" align="right" hspace="5" width="199" height="146">A 
reputable company with an unrivalled market presence, the AA Group of Companies 
specialises in catering and life support services. The Group comprises the 
following companies: Catering and Supplies Co. LLC, Oman Catering Co LLC, and 
Trading &amp; Catering Co LLC. An ex-associate of the worldwide Albert Abela 
Corporation, the Group was incorporated in Oman in 1968 and is presently made up 
of 100 per cent Omani-owned companies. <br>
<br>
Today, the AA Group is one of the largest contractors in the fields of catering, 
cleaning, laundry and related services. It operates the length and breadth of 
the country, providing services to both government organisations and private 
companies. <br>
<br>
The AA Group is part of Alawi Enterprises, a large and well-diversified Omani 
business house with interests in construction, oil and gas, travel and tourism, 
pharmaceuticals, insurance, auto services, and engineering and contracting.<br>
<br>
The group’s many years of operations in the Sultanate has endowed the company 
with a clear qualitative edge unmatched by any other contractor in the catering, 
cleaning and laundry business. Specifically, AA brings to its clients extensive 
knowledge and experience, qualified staff, established and tested management 
systems, and a well-oiled purchasing and transportation support structures.<br>
<br>
In addition to well-established management team/site managers and field 
supervisors, AA has full-time Hygiene Officers who regularly check on hygiene 
standards. Their principal tasks are to conduct hygiene courses and ensure that 
professional hygiene standards are consistently achieved. Besides, all the group 
companies are ISO 9001:2000 certified. Likewise, a comprehensive Health, Safety 
and Environmental protection scheme is in place. The company has an enviable 
safety record, which is often manifest in the form of ‘No Lost Time Injury’ 
achievements running into millions of man-hours.<br>
<br>
<b>Socat LLC</b><br>
<img border="0" src="im13.jpg" align="left" hspace="5" vspace="3" width="199" height="143">Socat 
LLC, established in 1985 as a subsidiary of French-based international catering 
company Universal Sodexho, is one of the leaders in Oman in Remote Site Catering 
&amp; Related Life Support Services, serving at over 100 locations throughout Oman 
with more than 2600 employees.<br>
<br>
Socat’s various services offered as part of site management include catering, 
housekeeping, cleaning, laundry, pest control, preventive maintenance, manpower 
supply, gardening &amp; landscaping, medical support, and fire fighting. Clients 
include oil and gas sector organisations, hospitals, schools, defence 
establishments, and businesses.<br>
<br>
Although 100 per cent Omani owned, Socat is 100 per cent managed by the world’s 
leading and largest multi-service provider, Universal Sodexho, a French-based 
international company operating in five continents. <br>
<br>
Sodexho was founded in 1966 and started its operations in France in the field of 
catering services to collectives, institutions, schools, hospitals, and so on. 
With the development of its activities, Sodexho is today the leader in France 
for catering and related services and has extended its operations to more than 
66 countries where the company provides a total of more than 5,000,000 meals per 
day.<br>
<br>
This position has been reached through Sodexho’s aggressive marketing techniques 
and constant efforts and strict policies in terms of cost and quality of 
services provided. Sodexho has been active in the Gulf area for 18 years, 
employing more than 5,500 staff from different nationalities. <br>
<br>
Sodexho’s history is characterized by a total dedication to the improvement of 
the quality of life in the institutions where it provides its services. This 
constant quest for improving the quality of services stems from the belief that 
if the food provided is good and the food is served with a smile, the worker 
living in a camp will be more relaxed, the pupil at school more attentive and 
the patient in the hospital more cheerful and hopeful towards life.<br>
<br>
The company is committed to maintaining technical and managerial excellence by 
employing highly motivated people through selective recruitment, comprehensive 
training programmes, empowerment of individuals and the application of 
performance-based recognition and reward systems. It also adheres to the highest 
levels of health, safety and environment protection norms, while embracing 
superior quality standards.<br>
<br>
Sodexho is now the largest operator in the sector of contract food services and 
the largest and leading company in remote sites management operations. Drawing 
its expertise and strength from this global player, Socat is well poised to 
strengthen its position in the Sultanate as a provider of world-class industrial 
catering services.<br>
<br>
<b>Kalhat Services &amp; Trading LLC</b><br>
<img border="0" src="im14.jpg" align="right" hspace="5" vspace="2" width="150" height="176">Kalhat 
Services &amp; Trading LLC, a subsidiary of Assarain Group of Companies, was the 
first company to offer commercial cleaning services in Oman. Launching 
operations on a modest scale in 1977, the company has since grown to become one 
of the largest providers of professional services covering a broad spectrum of 
specializations. <br>
<br>
Kalhat’s portfolio of activities includes contract cleaning of offices, banks, 
hotels, and industries; in situ carpet and sofa cleaning; waste management and 
environmental cleaning; high-rise window cleaning; chandelier cleaning; 
specialized cleaning of abattoirs, kitchens, pest control and termite 
treatments, specialized maintenance of marble and mosaic floors, and landscaping 
and garden services. <br>
<br>
Lately, Kalhat has been actively exploring and developing new business 
opportunities, notably in the design and implementation of aqua-scaping projects 
(waterfalls and water bodies), mountain streams, lagoons with Jacuzzi, automated 
sewer cleaning, industrial chemical cleaning, pickling, descaling, and 
passivation.<br>
<br>
Kalhat also has sophisticated cleaning equipment at its disposal, such as 
mechanical sweepers, compactors, beach cleaners and hydraulic platforms, among 
others. Along with the specialist skills of its staff, the company is 
well-geared to meet the specific requirements of its most demanding customers.
<br>
<br>
Today, the 28-year-old company boasts a workforce of around 2,300 
well-experienced staff, providing support services to clients across the breadth 
of the Sultanate. Besides Muscat, the company’s main areas of operation include 
Salalah, Sur, Sohar, Buraimi and Nizwa.<br>
<br>
Health, safety and environment considerations are ranked high among the 
company’s corporate priorities. Kalhat’s quality inspection team periodically 
inspects worksites to evaluate performance, as well as check on the health and 
safety of personnel. Human resources development gets priority attention within 
the organization. Refresher courses are provided for the benefit of cleaning 
staff at the company’s Training Centre in Ghala, while representatives of 
Kalhat’s principals organise on-the-job training programmes for staff. Kalhat 
has tied up with leading international providers of cleaning equipment and 
services, notably British Nova Works (UK), Rochester Midland Corporation (USA), 
Hako Minuteman (USA), Fakir Nilco (Germany) and Elite Crystal Services (UK). <br>
<br>
Attesting to the high professional standards that it constantly aspires for, 
Kalhat is also a member of the Carpet Cleaning Association (UK), British Pest 
Control Association and the International Sanitary Supply Association Inc (USA). 
These bodies provide advice on new advances and modern trends in cleaning 
services.<br>
<br>
Kalhat today counts among its client a high-profile lineup of government 
ministries, royal properties, educational institutions, military establishments, 
banks, embassies, hotel properties, tertiary hospitals, utilities, and large 
private organizations. With the rapid increase in investment in the industrial, 
commercial and tourism sectors, the company envisions a further rapid growth in 
business volumes.<br>
<br>
<b>Al Nab’a Services</b><br>
<img border="0" src="im15.jpg" align="left" hspace="5" vspace="3" width="199" height="190">Having 
launched itself into the big league of industrial contracting services 
providers, Al Nab’a Services has lately been successfully growing its share of 
the market. Established in 1986 as a franchise of the Service Masters Group, US, 
Al Nab’a has since emerged as a key force in providing cleaning services to the 
public and public sectors. <br>
<br>
The company’s portfolio of activities include cleaning and upkeep of premises, 
residences, buildings, market places, workshops, factories, showrooms, places of 
worship, specialised equipment, large vehicles, public places, gardens and 
parks, and so on.<br>
<br>
Al Nab’a is also engaged in providing specialised services such as pest, insect 
and termite control, polishing of marble and granite floors, minor maintenance 
jobs in electrical, mechanical and pipeline fields, and painting of residences 
and buildings. Part of the company’s 2,200-strong workforce is also trained to 
handle waste disposal, carpet shampooing and other such services.<br>
<br>
A prestigious line-up of government organizations and private sector firms has 
retained the professional contracting services of Al Nab’a. The list includes 
prominent government institutions, ministries, major banks, hypermarkets and 
large supermarket chains, shopping complexes, major automobile showrooms, 
hotels, private colleges, and the Sultanate’s first IT part – Knowledge Oasis 
Muscat (KOM).<br>
<br>
The company has built an enviable reputation around the delivery of professional 
pest control services – an area of expertise that few other contracting 
companies can rival. In fact, the name Al Nab’a has become synonymous with pest 
control services in the Sultanate, as large organizations and individual 
homeowners alike increasingly depend on Al Naba’s capabilities to fight insects, 
rodents and termites. These services are rendered by the company’s well-trained 
staff, who use quality pesticides and insecticides. <br>
<br>
Another dedicated team within Al Nab’a is trained to provide professional 
maintenance services in the form of carpet shampooing, floor polishing, car park 
and garden maintenance, and electrical, plumbing and carpentry works.<br>
<br>
Waste management is another key corporate strength. A number of private and 
public sector organizations have contracted Al Naba’s services to collect and 
dispose off waste from their premises. <br>
<br>
The company operates a fleet of highly mechanized skip loaded trucks as well as 
a pool of garbage removal vehicles. <br>
<br>
A periodical audit of quality standards ensures that Al Nab’a always maintains a 
superior edge to its range of professional services. Staff skills are kept honed 
through regular in-house training programmes, while health and safety issues 
receive due consideration as well. <br>
<br>
As a keen supporter of the government’s Omanisation initiatives, Al Nab’a has 
been working in partnership with the Ministry of Manpower to recruit, train and 
employ Omanis at all levels of the company’s organizational structure. A 
well-developed performance appraisal system ensures attractive rewards for top 
performers while several similar schemes have been introduced to motivate and 
encourage Omani staff. Company personnel also attend refresher-training 
programmes at a special training facility developed at the Ghala camp. </font></p>

