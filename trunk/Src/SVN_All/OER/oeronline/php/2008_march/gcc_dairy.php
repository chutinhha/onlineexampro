		<p align="justify"><b><font face="Verdana" size="4"><br>
		Public lessons from private equity</font></b><font face="Verdana" size="1"><br>
		<b>A new research by Booz Allen Hamilton opines that the public equity 
		sector would do well to follow the example of private equity firms in 
		the changing economic conditions of the Middle East&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
		</b><br>
		<br>
		<b>
		<img border="0" src="gcc_dairy.jpg" width="288" height="179" align="left" hspace="8" vspace="6"></b>Chief Financial Officers, the BOOZ Allen Hamilton study found, now face 
		a number of challenges and must be considered as business partners 
		actively managing value creation and influencing strategy, governance 
		and leadership. The study found that Private Equity (PE), due to a 
		number of factors, is growing in the region. The opening of regional 
		markets to competition is ushering a wave of strong economic growth in 
		the Middle East, and giving rise to favourable economic conditions – 
		including rising oil prices and maturing capital markets leading to 
		excess liquidity in the region – with the number of merger and 
		acquisition transactions (M&amp;A) rising to US$105 billion in 2007. 
		“Another pivotal factor is the fact that MENA private equity volume has 
		grown exponentially with more than US$20 billion managed or committed by 
		the end of 2006. In the first half of 2007, the average deal size was 
		US$149 billion and this is expected to increase further. Investors 
		increasingly recognise the benefits of private equity and with the trend 
		towards privatisation and the willingness to sell among family owned 
		companies, conditions are favourable,” said Ghassan Hasbani, 
		Vice-Principal with Booz Allen Hamilton. <br>
		<br>
		The study found that reports of successful MENA PE returns would prompt 
		even more activity in the future, especially after current successes. 
		“We found that successful private equity deals actually outperform the 
		best publicly traded companies,” he added.
		</font></p>
		<p align="justify"><b><font face="Verdana" size="1">New challenges</font></b><font face="Verdana" size="1"><br>
		The opening of regional markets and increased investment levels are 
		creating a new set of challenges for the finance function, the study 
		found, which is changing the nature of the Chief Financial Operator’s 
		(CFO) work. Such changes include privatisation, new regulatory 
		requirements, functional restructuring, the restructuring and review of 
		operations, operating model optimisation and business expansion. “We are 
		increasingly finding that CFOs are interacting with the CEO and focusing 
		less on transactional activities, a marked difference from CFOs’ 
		traditional role. While today, only 30 per cent of the CFO’s role is to 
		advise the CEO on strategy, revenues cost and corporate structure, in 
		18-24 months this role will increase to 63 per cent. In contrast, where 
		today 30 per cent of the CFO’s role is to understand costs and drive out 
		expenses, over the next 18-24 months this will decrease to only 10 per 
		cent,” Hasbani commented. “In addition, the transition to ‘business 
		partner’ typically involves more strategic activities with a change to 
		an external focus from an internal one, and a move to more strategic 
		business orientation, rather than operational,” he said.
		</font></p>
		<p align="justify"><b><font face="Verdana" size="1">Areas of focus</font></b><font face="Verdana" size="1"><br>
		CFOs can learn about this new active and strategic role from their 
		already active and strategic counterparts in private equity, the study 
		found. Governance, people and organisation, strategy and structure, and 
		operations are the areas that the CFO should involve so that his work 
		moves forward.<br>
        <br>
		“Essentially we can define the CFO’s function into three main fields - a 
		merger strategist, synergy manager and finance integrator. He should 
		guide the strategic and transactional aspects of a merger, monitor and 
		ensure synergy capture and manage the integration of the finance 
		function itself,” stated Hasbani. Consistently successful PE firms, the 
		study found, generally pursue the following: More frequent and informal 
		interaction with portfolio companies; more active involvement in 
		strategic as well as operational issues; higher degree of alignment of 
		incentives between PE firm and portfolio companies (higher equity share 
		to management); and, more reliable, timely and relevant information to 
		the PE firm. As a result, PE firm have a number of capabilities: More 
		industry and investment experience among the staff; fewer industries in 
		focus; and, higher ratio of investment professionals to capital under 
		management. CFOs must be business partners who actively manage value 
		creation, influence strategy, governance and leadership inside any firm. 
		Small changes in the finance function will lead to greater success such 
		as including an ambitious planning process, high speed in all 
		development initiatives, releasing the full potential of the 
		organisation through alignment, engagement and incentives, and 
		activating leadership. In addition, CFOs must be willing to sell when 
		there is no longer a way to create incremental above-market returns.</font></p>
		<p align="justify"><font face="Verdana" size="1"><b>Be Vigilant in 
		Monetary Policy: <br>
		</b>GFH Gulf Finance House’s GCC Economic Outlook for 2008, authored by the 
		company’s Chief Economist Dr Ala’a Al Yousuf, states that the outlook 
		for the GCC economy continues to be very positive but increased risks to 
		global economy mean that policy makers in the GCC have to remain 
		vigilant, especially in the conduct of monetary policy. <br>
		<br>
		Dr Al Yousuf said, “Although conditions in the global credit markets 
		have improved in recent months, we forecast a slower growth rate of the 
		global economy. Indeed, the global financial markets are assuming a 
		recession in the US and significant contagion to the rest of the world. 
		We think that this is too pessimistic. The US faces a prolonged period 
		of lower growth rate than its potential as the macroeconomic imbalances 
		and financial conditions gradually adjust towards sustainable levels. 
		Nonetheless, other major economies, such as the euro-zone and Japan, 
		have not seen the same excesses, and should hold up reasonably well. The 
		rapidly developing economies of Asia in particular, led by China and 
		India, should be able to de-couple at least partially from the US, as 
		they have done before.” <br>
		<br>
		The key risk to the GCC in 2008 outlined in the report is the spillover 
		of slower growth in the US to global demand for crude oil and hence 
		crude oil prices. Despite this, oil prices are expected to drift lower 
		towards US$75 per barrel by midyear, as increased supply catches up with 
		weakening demand and the dollar stabilises, but then pick up again in 
		the second half of the year. Based on these conservative assumptions for 
		the global economy, GCC-wide oil and gas export receipts are forecasted 
		to top US$450 billion in 2008, almost US$12,000 on a per capita basis. 
		The external current account surplus is also forecasted to hover around 
		US$200 billion and the fiscal surplus to be in the range US$150-170 
		billion. These surpluses are expected to boost government expenditure, 
		smoothing capabilities over the medium-term, and finance the spree of 
		cross-border acquisitions by sovereign wealth funds (SWFs). The second 
		key risk to the GCC highlighted in the report is stubbornly high 
		inflation, given the very limited scope for the use of monetary policy. 
		Inflationary pressure, however, are expected to moderate this year. 
		Global inflation fears are now overdone, and will not prevent major 
		central banks from responding to the downside risks to growth by cutting 
		interest rates. In the baseline scenario, the US Fed will slash the 
		Target Fed Funds rate to 2.5 per cent by mid-2008. The GCC states have 
		already slashed policy interest rates in response to the sudden Fed rate 
		cuts on January 22 and 31, 2008, and more cuts are expected to follow. 
		Accordingly, GCC governments are most likely to step up the use of 
		sterilisation operations via issuing central bank bonds/CDs as well as 
		direct credit/price controls, such as caps on loan-to-deposit ratios, 
		higher reserve requirements, subsidies, and caps on rental increases.<br>
        <br>
		Inflation rates will likely hover above the 5 per cent mark across most 
		GCC states and will be mostly driven by hikes in housing rents and food 
		prices. Real estate prices will stay firm as delays in project delivery, 
		rise in construction costs, rapid inflow of expatriates and growth in 
		mortgage finance accentuate supply-demand imbalances. Positive 
		supply-side responses, however, may partially subdue price hikes in 
		selected sectors. A series of positive supply responses are expected 
		towards the end of 2008 due to capacity additions by existing players, 
		intensifying competition amongst incumbents versus new entrants, and the 
		mere threat of entry. These dynamics will mostly play out in the 
		banking, transportation and telecommunications sectors. Given the 
		relatively low weight of these services in most GCC CPI baskets, 
		however, the impact of positive supply responses will be minor on 
		overall inflation levels.<br>
		<br>
		With regard to the exchange rate policies in the GCC, the dollar is 
		expected to weaken further against the euro to around US$1.55 in the 
		next few months before making a partial recovery. Therefore, the main 
		adjustment in the dollar’s value has already taken place. Speculation 
		about a possible concerted one-off revaluation of the currencies of the 
		GCC4 (excluding Kuwait and Oman) could return in the next few months.</font></p>
