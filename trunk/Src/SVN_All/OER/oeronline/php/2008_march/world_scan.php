<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b><img border="0" src="world_scan1.jpg" width="150" height="154" align="right" hspace="8" vspace="6">Spielberg pulls 
out from Olympics</b></font><font face="Verdana" size="1"><br>
Stephen Spielberg has withdrawn from his role as artistic adviser to the Beijing 
Olympics, explaining that his conscience over the Chinese Government’s 
involvement in the Darfur crisis prevented him from further participation. His 
move is a public relations blow to the Chinese government, which is under 
pressure to force the government of Sudan to resolve the crisis in Darfur. 
Spielberg’s worldwide profile could lead others involved in the Games to pull 
out and even lead sponsors to reconsider their roles in the event. As the 
biggest customer for Sudan’s oil, China has been the target of Darfur advocates 
for the better part of a year, much to the dismay of Chinese officials, who have 
complained that the Games were being politicised. <br>
