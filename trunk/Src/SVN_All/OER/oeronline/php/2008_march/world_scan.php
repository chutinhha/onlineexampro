<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>&nbsp;‘Oil crisis in 
next 10 years’</b></font><font face="Verdana" size="1"><br>
Oil companies, oil-producing countries and consumers need to act now to avoid 
the crisis that is coming within the next 10 years, feels John B. Hess, chairman 
and chief executive of Hess Corporation, a global integrated energy company. 
Speaking at the Cambridge Energy Research Associates’ annual energy conference 
in Houston, Hess predicted, “it is not only a matter of demand. It is not only a 
matter of supply…. We need to take steps on both fronts, and we need to start 
today”. Goldman Sachs Group Inc. estimates the number of on-road cars will soar 
to 500 million in China and 600 million in India by 2050. “That’s 1.1 billion 
vehicles in two countries that three years ago had fewer than 20 million cars in 
total creating an overwhelming increase in the need for automotive fuel,” said 
Hess.<br>
<br>
According to him the surge in oil prices in recent years has failed to weaken 
world demand because consumer incomes have grown faster than energy 
expenditures. “Since 1980, discoveries have not replaced our annual global crude 
oil production. Discoveries are getting smaller and located in more difficult 
environments, such as the deepwaters of the Gulf of Mexico, Brazil and West 
Africa, where companies are now drilling in depths of up to 7,000 ft and 
searching for targets that are in some cases more than 30,000 ft deep,” Hess 
said.<br>
