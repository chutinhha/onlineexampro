<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Heathrow Terminal 5 to 
open in March</b></font><font face="Verdana" size="1"><br>
</font>
<img border="0" src="world_scan.jpg" width="288" height="163" align="right" hspace="8" vspace="6"><font face="Verdana" size="1">Terminal 5 at London’s Heathrow Airport, which will exclusively serve British 
Airways flights, will open on March 27. According to Paul Starrs, British 
Airways’ Commercial Manager for the Middle East, “Everyone at British Airways is 
very excited about Terminal 5, an investment worth 4.3 billion GBP. We wanted to 
make any journey via Heathrow smooth, simple and stress-free, so we made 
Terminal 5. It’s all about putting the passenger first.<br>
<br>
“We have speeded up the processes that make spending time at an airport a chore, 
so there is more time for relaxing, eating, shopping or working. That means less 
time spent checking in through the extensive check-in facilities, including 96 
self-service kiosks and 96 fast bag-drop desks. There will be extensive retail 
facilities on offer, with 144 stores and restaurants. We have got some of the 
best names in the business at Terminal 5, such as Gordon Ramsey’s Plane Food, 
which is his first airport restaurant, as well as retail outlets, ranging from 
Paul Smith, Tiffany’s, Prada, Mulberry, to the first-ever Harrods fashion 
concept store,” opined Starrs.<br>
<br>
“We have also taken the opportunity with Terminal 5 to radically overhaul our 
airport lounges, or ‘Galleries’ as they are now known. There are six lounges for 
our First and Club World customers: The Concorde Room, The First class lounge, 
three Club Lounges and an Arrivals Lounge, with a capacity to cater to 2,500 
passengers, making it the largest and possibly the most luxurious in the world. 
For the ultimate in pre-flight pampering, we have teamed up with Elemis, the 
leading skincare expert, to offer customers specially created spa treatments,” 
Starrs concluded.<br>
