<p align="justify" style="margin: 3px 10px">
<font size="2" color="#CC3300" face="Verdana"><b><br>
Indian IT-ITES industry continues to boom </b></font><font face="Verdana" size="1">
<br>
The total revenue of India’s IT &amp; IT enabled services (ITES) industry is 
expected to be $47.8 billion this year up from $37.4 billion last year, 
according to India’s National Association of Software and Services Companies (Nasscom). 
Software and services export continues to grow at around 30 per cent per annum 
and has reached $31.4 billion. The contribution of the IT-ITES industry to the 
country’s GDP grew to 5.4 per cent from 4.7 per cent of last year, thus 
providing employment to more than 1.6 million youth, it said. However, the US 
credit crisis and appreciating rupee are posing strong challenges to the Indian 
IT industry. The once darling of investors, Indian IT companies have lost their 
sheen in the stock market. While traditionally Indian IT bigwigs such as Infosys, 
Wipro and TCS have concentrated on the US and European market for their 
businesses, the multinationals, notably IBM, have made significant inroads in 
the domestic market. IBM today has one-fifth of its workforce in India, 
accounting for the largest employee base next only to the US. Not only has IBM 
increased headcount, but also received large multi-million dollar orders from 
domestic clients.<br>
