</font><b><font face="Verdana" size="4">PET project -a landmark for Port Sohar</font></b><font face="Verdana" size="1"><br>
<b>The PET project only confirms that the Sohar Industrial Zone is growing into 
one of the world’s biggest greenfield petrochemical hubs<br>
</b><br>
Oman’s first-ever PET project, coming up in Sohar with an investment of around 
US$210 million, promises to strengthen the country’s credentials as a global 
producer of petrochemicals. The Sohar Industrial Zone itself, with a number of 
gas-based and downstream energy related projects in the pipeline, is clearly 
growing into one of the world’s biggest greenfield petrochemical hubs. Port 
Sohar is also at the core of a US$12 billion integrated economic development 
plan and is expected to be crucial for the success of this petrochemical 
cluster. It enjoys a strategic position outside the Strait of Hormuz and lies in 
the vicinity of the vibrant shipping arteries of the Gulf of Oman.<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img border="0" src="inthenews3.jpg" width="331" height="159" hspace="0" vspace="4"><br>
<br>
For this PET project, the land will be secured by the promoters around the Sohar 
Port, with the Sohar Industrial Port Company (SIPC), the port authority, 
allocating around 80,000 sq. m. Land documentation procedures are expected to be 
finalised in the first week of March. Euro Consult FZC, an Abu Dhabi-based 
business, industrial and financial consultancy firm, and Centuria Capital, a UAE-registered 
firm owned by Centuria Bank of France, are the lead promoters and partners in 
the PET project. <br>
<br>
The project will source its feedstock requirements from Aromatics Oman’s 
US$1.6-billion world-scale plant, currently under construction in the Port of 
Sohar. The feedstock will comprise PTA--purified terephthalic acid (80 per cent) 
and ethylene glycol (20 per cent). The venture is a link in the petrochemical 
value chain, paving the way for fresh opportunities for downstream investment. 
PTA requirements can be sourced internationally also, feels Dr. Faouzi. A. 
Reslan, partner and managing director of Euro Consult. <br>
<br>
This high-tech plant will have an annual capacity of 140,000 tonnes of 
polyethylene terephthalate (PET), which is used as a raw material for 
manufacturing PET bottles and containers. These PET products will go to bottling 
companies across the globe, thereby introducing Oman to the global consuming 
market. <br>
<b><br>
Demand for PET</b><br>
The global demand for PET is enormous. Currently, estimated at around 20 million 
tonnes, it is growing at a robust 10 per cent per annum, showing its huge market 
potential. Once operational in 2010, the project will give competition to Saudi 
Arabia’s SABIC, the only other producer of PET in the Gulf region. It is learnt 
that the promoters of the Sohar plant have already entered into an offtake 
agreement with the technology suppliers for 100 per cent of the plant’s PET 
output. <br>
<br>
The funding for this project is being done by a combination of equity of 30 per 
cent and debt of 70 per cent. Other investors will primarily be GCC firms and 
individuals. The company is likely to float an IPO 2-3 years after operations 
start to attract Omani investors. <br>
<br>
<b>Spin–off venture</b><br>
Also, in March, the promoters of the PET project and Oman’s Sami Abdul Aziz 
Jaffer Group plan to float a spin-off venture based on polypropylene. The two 
parties are currently developing Oman’s first-ever non-wovens’ plant in Sohar 
Industrial Estate at a cost of around Euro 50 million. Neumag of Germany is 
bringing cutting edge equipment for the non-wovens’ project for which building 
solutions specialists, Hoops + Hilgendorff of Germany, are the industrial 
consultants while Abdulla Mukadam is the local consultant. The plant, with an 
initial capacity of 17,000 tonnes per annum, will be the only manufacturing unit 
of its kind in the Gulf region, save for a limited-scale non-wovens’ venture in 
operation in Saudi Arabia. It aims to source its raw materials from the 
polypropylene project. </font></p>
