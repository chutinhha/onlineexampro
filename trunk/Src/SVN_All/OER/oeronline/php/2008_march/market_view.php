<b>National Bank of Oman SAOG:</b> NBO reported a 46.6 per cent growth in net profits 
(provisional results, subject to CBO approval) for 2007 on the back of robust 
growth in non-interest income. This was also aided by good recoveries in the 
last quarter, and a lower operating expense as compared to previous years. Net 
interest income (YoY) grew over 11 per cent to touch RO39.1 million. Against 
this, on the income (non-interest income) side, the bank achieved 49.5 per cent 
growth YoY to RO26 million, a key component in 2007 to the bank’s growth in its 
bottomline. Commission and fees constituted about 63 per cent of the other 
income component and about 9 per cent was contributed by treasury and forex 
earnings. <br>
<br>
The bank’s operating expenses rose by about 21.6 per cent during the year, 
mainly driven by a 38 per cent rise in administration costs at RO8.6 million. 
However, due to its growing income stream, the cost-to-income ratio shows an 
improvement at 44 per cent as compared to 45 per cent in 2005. The 26 per cent 
growth in operating profits (RO36.4 million) also factors in the impressive 
performance achieved on the non-interest income component. Provision for loan 
losses was down sharply by 26 per cent while recoveries rose by about 33 per 
cent. In 2007 as well, the Bank maintained its focus on remedial accounts and 
its efforts were rewarded with recoveries exceeding RO21.8 million (RO12 million 
loaded in the last quarter). In fact, net recoveries of RO14 million also helped 
in a big way in boosting the bottom line (about 28 per cent of pre-tax profits). 
Despite severe competition from existing as well as new players, the bank posted 
good asset growth. For the full year 2007, the bank’s loan book grew by 29 per 
cent to RO907 million, with an addition of about RO203 million from December 
2006 levels.Deposits grew slower, up 15 per cent at RO936 million, though at a marginally 
higher cost.<br>
<br>
Given our assumptions of a 30 per cent growth in loan assets in 2008, we expect 
the NBO to generate an EPS of RO0.584 and BV per share of RO3.142. Current 
valuations discount the EPS estimate by 13.9 times and BVPS estimate by 2..6 
times. Though competition remains a concern, we think the softening rates 
