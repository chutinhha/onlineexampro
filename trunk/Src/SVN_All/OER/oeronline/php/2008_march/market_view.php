</font><font size="4"><b>MSM stays the course</b></font><font size="1"><br>
<b>The month ended February 15 was a tough period for most of the GCC bourses, 
which shed weight during the month<br>
</b><br>
The MSM30 index went into a consolidation phase–declining by 1 per cent–during 
the period January 16 to February 15 after five months of sustained upward 
movement. This was even though on January 22, the index recorded its 
biggest-ever one-day fall of 8.33 per cent, with the banking and insurance 
indices leading the crash, losing 3.5 per cent due to worries over recession in 
the US, which led to heavy selling across global markets. Bank Muscat and Gulf 
Investment Services led the decline in the banking and insurance sector index. 
The recovery from January 22’s lows was mainly because of active buying interest 
in the industry and services sector stocks. Industry index companies leading the 
recovery included Al Jazeera Steel Products (+7 per cent), Oman Cable Industry 
(+10 per cent) and Oman Cement (+5 per cent).<br>
<br>
In other corporate developments, Galfar Engineering declared that it had bagged 
RO86.9 million contract for A’Seeb coastal vacuum sewer networks and ancillary 
works from Oman Wastewater Services Company SAOC. The contract period is around 
39 months. In another major development, Bank Muscat announced its plans to 
acquire majority stake in Saudi Pak Commercial Bank in Pakistan in association 
with Sinthos Capital, International Finance Corporation, Washington, and Nomura 
International. Bank Muscat will have a 35 per cent stake in the bank while 
collectively the consortium will hold 68 per cent stake. The acquisition is 
awaiting approval from the State Bank of Pakistan. <br>
<br>
<b>GCC Market</b><br>
The month ended February 15 was a tough period for most GCC bourses, which shed 
weight during the month. However, the Kuwaiti and Bahraini bourses managed to 
close on a positive note, gaining 2.64 per cent and 1.02 per cent, respectively. 
The insurance index was the top gainer on both the bourses, which ended up 7.81 
per cent and 3.21 per cent, respectively. The Saudi market was the worst 
performer, with the general index there shedding 22.14 per cent. All the broader 
indices here were in the red, with the industrial index taking the lead and 
slumping 26.57 per cent during the month. <br>
<br>
The DFM was the next worst performer losing 8.84 per cent. The share prices of 
the area’s largest real estate company, Emaar Properties, dwindled on the back 
of lower net profit and after its Indian IPO was deferred, and it took the real 
estate index down by 20.25 per cent. The Doha market too occupied the list of 
the losers, with the general index here plummeting by 7.28 per cent. All broader 
indices here were in negative territory, with the insurance sector leading the 
convoy by losing 21.13 per cent. The Abu Dhabi bourse shrank 3.18 per cent, with 
the banking counters leading the loss-makers.<br>
<br>
<b>Research Highlights<br>
Shell Oman Marketing Company:</b> Shell Oman Marketing Company (Shell) reported 
another strong full year results, with 22 per cent increase in revenue and an 
operating margin expansion of 1.13 per cent YoY. Excluding one-time expenses 
(donation- RO250,000), EPS for the year stood at 113 Bz compared to 89 Bz in 
FY06. <br>
<br>
Growth in the economy and increasing capital expenditure favourably impacted the 
commercial and retail segments of the company. However, compared to the 
industry, Shell’s performance was below par and this was mainly attributable to 
fuel-pricing irregularities between Oman and the UAE. Shell, however, continued 
to experience strong growth in the retail segment, as was evident from its 
average higher throughput (ATP), which grew by 8 per cent in 2007 to 9.3 million 
litres per annum. The increasing economic activity on the back of strong oil 
prices fuelled growth in local consumption. Of the total Oil production, 
approximately 15 per cent of Oman oil production is consumed internally 
(approximately 4 billion litres). <br>
<br>
During the year, Shell opened four new retail outlets and two new outlets are in 
the process of completion. During the year, Shell’s total retail outlets 
numbered 126, with approximately 50 select stores (at petrol pumps). Retail 
still remains the highest revenue contributor for Shell. This is even though 
Shell’s market share has been eroding due to increasing competition and higher 
growth registered by competitors. However, Shell remains the market leader with 
50 per cent retail market share. As a whole, the oil marketing sector looks 
attractive due to robust economic conditions, and operationally Shell’s 
performance compared to its peers remains good. We expect Shell to benefit from 
the increasing capital spending on infrastructure and construction, over and 
above on the rising auto sales in Oman.<br>
