</font><font color="#CC3300" face="Verdana"><font size="2"><b>Reliance Money 
eyeing Oman in 2008</b></font></font><font face="Verdana" size="1"><br>
Reliance Money, the financial service and product distribution arm of leading 
Indian business entity Anil Dhirubhai Ambani Group, which launched its broking 
and portfolio management services in the UAE, is planning to enter into 
partnerships in Oman and other Gulf countries. The company is looking to tap the 
population of 20 million NRIs and Indians residing in the Middle East. <br>
