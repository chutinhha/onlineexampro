<font size="2" color="#CC3300" face="Verdana"><b>Oman Air to increase flights to 
India<img border="0" src="business_briefs1.jpg" width="275" height="98" align="right" hspace="8" vspace="6"></b></font><font face="Verdana" size="1"><br>
The Directorate-General of Civil Aviation and Meteorology has signed a 
memorandum of understanding (MoU) with the Indian aviation authorities to 
increase the rights and number of destinations of Oman Air in India. Rashid bin 
Mohammed Al Kiyumi, assistant director-general for civil aviation services at 
the directorate, said the two sides have agreed to increase the number of seats 
on Oman Air flights to India from 7,550 to 11,550. “The increase in the number 
of seats will increase the number of weekly flights operated by Oman Air to 
Mumbai from 10 to 14 flights; to Delhi from five to seven; to Lucknow from four 
to seven and Jaipur from four to six,” Kiyumi informed.<br>
<br>
“The MoU states that with the addition of Bangalore and Kolkata, the number of 
Oman Air destinations in India has increased to 10,” Kiyumi added, noting that 
the MoU would enable India’s Jet Airways, that started operations in the 
Sultanate recently, to increase its flights to Muscat soon. <br>
<br>
</font><font color="#CC3300" face="Verdana"><font size="2"><b>Swiss-Belhotel 
launches two new properties</b></font></font><font face="Verdana" size="1"><br>
Swiss-Belhotel International plans to develop their seventh hotel in the Middle 
East region – Swiss-Belhotel Al Qurm, Oman – as part of an agreement with 
Dubai-based HBG Holdings Ltd. “Coming quickly on the heels of the acquisition of 
Sohar Beach by Swiss-Belhotel, we are delighted to announce our second property 
in Oman, Swiss-Belhotel Al Qurm. We have an aggressive expansion plan in place 
for the group with particular emphasis on the Middle East region.” said Gavin 
Faull, President, Swiss-Belhotel International.<br>
Located in Muscat, Swiss-Belhotel Al Qurm will be a multi-purpose hotel complex 
comprising two towers. Tower 1 will feature a 179-room 4 star hotel, while Tower 
2 will feature 120 residential apartments, three floors of commercial office 
space, spa and health club facilities. According to Edgar Solenthaler, VP 
Operations &amp; Development, Middle East, Swiss-Belhotel International, the 
property will be contemporary in design with distinctive Omani touches. “The 
multi-function nature of the property lends itself to both leisure and corporate 
guests. The hotel will be well equipped for larger families with 25 per cent of 
the total rooms inter-connecting,” said Solenthaler.<br>
