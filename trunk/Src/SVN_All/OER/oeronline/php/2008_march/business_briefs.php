</font><font color="#CC3300" face="Verdana"><font size="2"><b>New identity for 
Fund for Development of Youth Projects</b></font></font><font face="Verdana" size="1"><br>
As a reflection of its new vigour, the Fund for Development of Youth Projects 
has adopted a new identity under the brand name ‘Sharakah’. The Sharakah 
programme is the only venture capital fund dedicated for SMEs in Oman where 
projects supported by the Fund have an automatic five-year tax break and are 
also entitled to soft loans. On the new strategy, Malak Al-Shaibani, General 
Manager of Sharakah, said, “We will continue with our commitment to assist Omani 
entrepreneurs through our funding and advisory services. The Fund has undergone 
a complete evaluation and re-positioning exercise to ensure that the right 
mechanics, systems and procedures are in place to maximize results for the 
entrepreneurs and the Fund.” <br>
Al-Shaibani added that in 2008, Sharakah will focus on a diverse range of 
sectors including hospitality (restaurants, coffee shops, motels), leisure and 
entertainment, down stream industries, information technology, education and 
training in addition to support services to the construction, tourism, and oil 
and gas industries.<br>
