</font><font color="#CC3300" face="Verdana"><font size="2"><b><br>
Korean giants get over $1.5bn contracts</b></font></font><font face="Verdana" size="1"><br>
Hyundai Heavy Industries, the world’s largest shipbuilding company, has been 
given US$767.6 million order to build five supertankers for Oman Shipping 
Company. According to a Hyundai statement, it would deliver the ships by 
December end in 2011. On the other hand, Daewoo Shipbuilding and Marine 
Engineering, the world’s third-largest shipbuilding company, has won an order 
worth US$770 million to build five Very Large Crude Carriers (VLCCs) for Oman 
Shipping Company. The delivery of the first VLCC is expected in December this 
year.<br>
