</font><font color="#CC3300" face="Verdana"><font size="2"><b>OQIC-Euler Hermes 
to launch new insurance products</b></font></font><font face="Verdana" size="1"><img border="0" src="business_briefs2.jpg" width="275" height="156" align="right" hspace="8" vspace="6"><br>
Oman Qatar Insurance Company (OQIC) has introduced Trade Credit Insurance 
solutions, a first of its kind in the insurance industry in Oman. OQIC has 
partnered with Euler Hermes, the world’s largest and leading credit insurer. 
Euler Hermes recently set up a full-fledged operation in Dubai to enable direct 
services to the GCC clients. “The local team will be an integral part of the 
Euler Hermes network. The team will work on our risk database and our clients 
will have an online access to it. From the first day, our policyholders will 
enjoy the same quality of service as anywhere else in the world,” said Jef 
Vincent, Regional CEO Asia of Euler Hermes. <br>
<br>
OQIC General Manager Thomas Devasia said, “Businesses in Oman are quickly 
integrating with the global marketplace. There are also an increasing number of 
Omani businesses that are multinational in their operations. Credit risk 
exposures in trade are real and need to be understood and managed. This 
insurance can also be seen as a financial risk management tool which enables the 
right balance between commercial aggressiveness and financial prudence. 
Uniquely, it can address both domestic and international trade credit risks.”<br>
