</font><font size="2" color="#CC3300" face="Verdana"><b>Nawras claims huge</b></font><font color="#CC3300" face="Verdana" size="1">
</font><font size="2" color="#CC3300" face="Verdana"><b>MNP success</b></font><font face="Verdana" size="1"><br>
Nawras has reported an overwhelming response to its mobile number portability (MNP) 
facility. MNP is a service that allows corporate and individual customers to 
move from one service provider to another without losing their existing mobile 
numbers. According to a press statement from Nawras, nine out of 10 customers 
that opted for MNP in 2007 have chosen Nawras.<br>
</font><font color="#CC3300" face="Verdana"><font size="2"><b><br>
TCS BaNCS Securities Trading Solution for BankMuscat</b></font></font><font face="Verdana" size="1"><br>
TCS Financial Solutions, the strategic business unit of Tata Consultancy 
Services, dedicated to providing business application solutions to the banking, 
insurance and capital markets industries, will provide BankMuscat TCS BaNCS 
Securities Trading solution to assist its customers trade online in the Muscat 
Securities Market.<br>
<br>
</font><font color="#CC3300" face="Verdana"><font size="2"><b>Octal targets 
US$200 million sales to Europe</b></font></font><font face="Verdana" size="1"><br>
Oman-based Octal Petrochemicals is aiming for sales of US$200 million to Europe 
this year as it prepares to attend Interpack, which takes place in Dusseldorf, 
Germany, from April 24-30. It is the world’s largest event for the packaging 
industry. Octal expects total sales of its APET (amorphous polyethylene 
terephthalate) sheet packaging and PET resin to reach US$500 million per annum 
by the end of 2008, around one-fifth of the world market for APET.<br>
