</font><font color="#CC3300" face="Verdana"><font size="2"><b>Oman Oil to power 
Jet Airways<img border="0" src="business_briefs4.jpg" width="194" height="69" hspace="8" vspace="6" align="right"></b></font></font><font face="Verdana" size="1"><br>
Oman Oil Marketing Company (Omanoil) has bagged the contract to supply jet fuel 
