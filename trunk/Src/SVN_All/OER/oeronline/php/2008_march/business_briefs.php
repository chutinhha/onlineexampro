</font><font color="#CC3300" face="Verdana"><font size="2"><b>Omran-ESO 
initiates recycling programme<img border="0" src="business_briefs5.jpg" width="175" height="135" hspace="8" vspace="6" align="left"></b></font></font><font face="Verdana" size="1"><br>
Omran and Environment Society of Oman (ESO) have launched a unique recycling 
programme in the Sultanate. Twenty recycling bins will be placed in fourteen 
international and private schools in Muscat with an estimated number of 13,000 
students participating in the initiative. In recent years, recycling has gained 
great impetus in regional and international communities due to its proven 
results of preserving forests, conserving resources and reducing pollution and 
solid waste. On the occasion, Tim Redman, Chief Operating Officer of Omran, 
said, “Tourism and the environment are closely interlinked and we are proud to 
be working with ESO to increase awareness on the importance of recycling to 
facilitate the transfer of knowledge for sustainable development”. Nida Helou, 
Manager of ESO, said, “We must not underestimate the impact each individual has 
on making a difference and with this programme, we hope to foster a new 
generation that is empowered with the knowledge and passion to save the 
environment by integrating simple recycling behaviours in their daily lives”.
