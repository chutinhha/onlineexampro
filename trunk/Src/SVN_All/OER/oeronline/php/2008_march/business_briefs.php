</font><font color="#CC3300" face="Verdana"><font size="2"><b>Omani-Italian JV 
to promote luxury resort </b></font></font><font face="Verdana" size="1">
<img border="0" src="business_briefs3.jpg" width="258" height="154" hspace="8" vspace="6" align="left"><br>
The Ministry of Tourism has signed an agreement with World In for the 
development of Hayoot Beach Resort in Musandam. World In is promoted by HE Abdul 
Alim Bin Mustahail bin Naseeb Rakhyoot and Paolo Bolici of Italy. The luxurious 
coastal resort will provide super deluxe accommodation in 159 exclusive cottages 
and villas located 3.5 km east of the township of Khasab in the Musandam region. 
It will be spread over an area of 215,425 sq m. The new property is expected to 
create uncompromising standards and perception of luxury, style, intertwining 
nature, history and innovation to attract international tourists especially from 
Europe. <br>
Paolo Bolici is the owner of Bolici Group, a holding company with subsidiaries 
including marine &amp; civil construction, interior design, real estate development, 
investments in hotel, luxury yachts building, cruise ships interior, marina 
hotel investments &amp; developments and engineering consultancy. Bolici Group is 
the owner of Wonder Travel, a tour operator and Destination &amp; Leisure Management 
Company based in Rome.<br>
