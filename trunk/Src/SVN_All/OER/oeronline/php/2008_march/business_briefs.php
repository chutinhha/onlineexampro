</font><font color="#CC3300" face="Verdana"><font size="2"><b><br>
PRD nationwide poised for major growth
<img border="0" src="business_briefs6.jpg" width="175" height="140" align="right" hspace="8" vspace="6"></b></font></font><font face="Verdana" size="1"><br>
PRDnationwide, Australia’s premier project marketing real estate company, 
celebrating its four years of successful operations in Oman is expanding its 
operations in the region. Based in Muscat, PRDnationwide has residential project 
marketing and research skills and extensive commercial property marketing 
expertise. “The real estate market in Oman is rapidly changing and is expected 
to gain more impetus in the years to come,” expressed James Fox, Managing 
Director of PRDnationwide MENA. “By utilizing a fully-integrated project 
marketing services philosophy, we are able to provide a holistic understanding 
to the real estate and integrated tourism development industry with a one stop 
shop approach, thereby adding value throughout the development process combined 
with unparallel quality of service by our qualified and experienced workforce.”<br>
<br>
James added that the foreign ownership law will continue to be a significant 
driver of the demand for real estate in the future, which expands foreign 
ownership rights formerly restricted to GCC nationals, to include non-GCC 
nationals as well. With the Government’s ambitious diversification plan in the 
industrial and tourism segment, James is confident that the industry will 
continue to flourish. PRD’s first project in Oman was the planning, marketing 
and sales of the Barr Al Jissah Residences adjoining the Shangri-La’s Barr Al 
Jissah Resort and Spa for the Zubair Corporation. The project has received 
international acclaim for the first phase of Residences and now marina 
waterfront apartments are planned for release in mid 2008, with retail and 
restaurants on ground level. With an ambitious development plan, PRDnationwide 
will be launching offices in other GCC states beginning with a planned opening 
in Dubai in 2008.<br>
<br>
</font><font color="#CC3300" face="Verdana"><font size="2"><b>Omantel &amp; Oman 
Mobile move towards unified management</b></font></font><font face="Verdana" size="1"><br>
Oman Telecommunications Company (Omantel) and Oman Mobile Communications Company 
(Oman Mobile) have initiated the plan to develop performance as well as steps to 
integrate the managements of both companies. These efforts are part of both 
companies’ plans to enhance the working environment under a unified management, 
with the objective being to cope with the next phase in telecommunications 
sector in the Sultanate in general and in both companies in particular. HE 
Engineer Sultan bin Hamdoun Al-Harthi, the Board Chairman of Omantel and Oman 
Mobile, indicated that this move is part of the implementation of the 
recommendations of the Ministerial Committee, established to develop the 
performance of both Omantel and Oman Mobile.<br>
<br>
HE Hamdoun added that the managements of both companies have already started to 
take steps towards merger and the development of the working environment in 
order to realize future aspirations and visions. The objective of this endeavor 
is to develop the company, and achieve the maximum benefit from their combined 
resources and potentials towards the realization. It also aims at establishing 
an agenda for collective growth and identifying the best ways and means to 
improve the general performance. The first phase of the development and 
modernization process, which started from 17 February, is expected to be over by 
the end of the year. The focus will be on integrated operation in order to best 
achieve the synergies between the businesses, build internal capabilities and 
improve operational efficiencies. He said that the Board of Directors would 
continue to be closely involved in these efforts in order to achieve success, 
which is a top priority.<br>
