<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">OER CEO Golf 2008 <br>
A Grand Finale<img border="0" src="golfupdate.jpg" width="199" height="368" align="right" hspace="8" vspace="6"></font></b><font face="Verdana" size="1"><br>
<br>
The 4th edition of OER CEO Golf 2008 held at the Ghalla Wentworth Golf Club on 
Thursday, February 21 drew an enormously positive response from the crème-de-la-creme 
of corporate Oman. The event was a stupendous success. Whether it was the 
profile of participants, the gamut of exciting activities, the fabulous prizes 
and the lavish hospitality – all together made OER CEO Golf 2008 a luxury golf 
tournament worth remembering off the golf course.<br>
<br>
More than 200 corporate captains of Oman, came together to enjoy a day of 
leisure on the golf course. For the head honchos of Muscat, it was a pleasant 
opportunity for networking and doing business outside the boardroom while 
relaxing. In other words, OER CEO Golf is an integral part of business 
networking. <br>
<br>
In the soft morning light, Lars Gustaffson, Head of Marketing and Sales Division 
of Oman Mobile, Hani Al Zubair, Director Zubair Corporation, Salah Murad, Deputy 
CEO, Ahli Bank and Sandeep Sehgal, GM, United Media Services did the ceremonial 
putting. OER CEO Golf 2008 started off with the golfers playing the 18-hole 
Stableford Format. Golfers-to-be, meanwhile, joined professionals Brandon 
D’Souza, Rani Sonti and others to get tips on golf and know the finer points of 
the game and squared off in an exclusive 4-hole round of golf. They took their 
first shot on the turf with much ado. <br>
<br>
After a sumptuous lunch where the networking and camaraderie continued in right 
earnest, participants left for a quick siesta before returning in the evening 
for the exhilarating, action–packed evening. <br>
<br>
From 6:30pm till late hours, the scene of action shifted indoors, where golfers 
and golfers-to-be teamed up for experiencing for the first time ever in Oman- 
Virtual Golf, an indoor golf simulator, to be precise. They together played on 
the GolfBlaster3 system that combined top spec technology, realistic graphics 
and superb special effects on world renowned golf courses. It was an enjoyable 
and memorable experience for all who participated. <br>
<br>
While their spouses revelled in the nitty gritties of their favourite sport, the 
ladies had their own share of fun. They had their special corner for playing 
games where the stakes were high and the prizes simply fabulous. A special 
entertainment troupe from Latin America was specially flown in for the occasion 
to present foot-tapping dance numbers to astounding music. <br>
<br>
The day’s programme concluded with the distribution of prizes which were more 
extravagant than the previous editions. The enticing prizes were Rolex from 
Khimji’s Watches, Segway from Zubair Automotive, Blackberry from Oman Mobile, 
Bose Lifestyle 48 from OHI Electronics, Perfumes, Personal Accessories and 
Waterman Pens from Capital Store, Samsonite and other products from Khimji’s 
Luxury &amp; Lifestyle, Fuji Cameras from Photocentre ,Video Door Phone Access 
system from Majees Technical Services and sit down dinner invitation with Jack 
Welch.<br>
<br>
Mudit Saxena, Assistant General Manager, Consumer banking, Bank Dhofar and 
Sachin Bawa, CFO Shanfari Group of Companies, bagged the champion’s trophies and 
prizes for being the nett and gross winners, respectively. Moreover many prizes 
were won by the other participants for other categories and contests.<br>
<br>
OER CEO Golf, this year too, has forged strategic partnerships with leading 
brands in the Sultanate. It was presented by Blackberry from Oman Mobile in 
association with Mitsubishi Motors and Zubair Automotive LLC and Ahli Bank and 
partnered by National Car Rental, a division of Shanfari Group of Companies. 
Rolex from Khimji’s Watches was the official time keeper.<br>
<br>
The media partners for this event were Times of Oman, Al Shabiba, Hi and Al Youm 
Al Sabe. Category partners included DHL for Logistics, Ruwi Modern Printers as 
print partners, AXA Insurance as insurance partner, Infoline as call centre, Al 
Sawadi Beach Resort and Flavours restaurant as Hospitality partners and OUA as 
the Beverage partner. <br>
<br>
The corporate game is clearly getting popular in Oman as the number of golfers 
is increasing every year and more golf courses are coming up. The camaraderie 
and team spirit of the high profile participants and partners has taken the 
event to new heights. OER CEO Golf 2008 is not just a golf event but a complete 
show that 
is organised by Applause Events, an event management specialist in Oman. In 
partnership with Oman Economic Review (OER), Oman’s leading business and economy 
magazine, it has established new benchmarks for high-profile corporate events in 
the Sultanate of Oman.<br>
<br>
Virendra Agarwal, CEO, Moosa Abdul Rahman Hassan &amp; Co. said “I am playing golf 
for 32 years in Oman. It has proved to be popular among corporate chiefs and 
professionals who are showing a keen interest in the game. Once Oman begins to 
sport green courses, we can have great shows with golf professionals and 
amateurs from other regions.”<br>
<br>
Saibal Basu, General Manager-Oman of Landmark Group felt, “Events like this are 
commendable not just to re-energise ourselves but also for the relationships 
that are strengthened based on a shared experience. It is the best place for 
business interaction.”</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
