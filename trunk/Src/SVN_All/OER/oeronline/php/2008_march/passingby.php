<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The Business of Making Cinema</font></b><font face="Verdana" size="1"><br>
<b>Sanjay Srinivas, founder and CEO of Chequered Flag Entertainment Company, is 
an MBA by qualification and a storyteller by passion. He mingled business with 
passion and came up with many creative projects in the genre of world cinema. 
Very recently, he was in Muscat to generate funds for his forthcoming film God 
Lives in the Himalayas</b><br>
<br>
<img border="0" src="passing.jpg" width="288" height="244" align="left" hspace="8" vspace="6">Filmmaking is both a profession and a passion for Sanjay Srinivas. At present he 
is making cinema aimed at global audience. His approach is different from 
majority of filmmakers in India who target the Indian market and the Diaspora. 
His company intends to make films that go through the festival circuits before 
they get sold by distributors across the globe. So the idea is to tell a local 
story to an international audience. That is what is called world cinema, which 
doesn’t happen in India.<br>
<br>
The film God Lives in the Himalayas is his first such project, shot in and 
around Nepal. It is an Indian production made in Nepali language, meant for 
global audience. He strongly believes filmmaking is all about story telling. 
When he was young, he amused people by telling great stories of all types. Today 
he writes, produces and directs plays, documentaries and corporate films. His 
previous feature film, The Dressing Room, received rave reviews. <br>
<br>
His upcoming project is about the adventure of four Nepali Sherpa kids trying to 
climb Mount Everest to meet God. The crew is Indian and shooting starts 
mid-February at the base camp of the mountain. The film will be launched at the 
Cannes film festival in France in May. From there, Sanjay will first take it to 
several festivals and then come to India and Nepal.<br>
<br>
The total project cost is worth US$ 640,000. The company is contributing 
US$63,000. For this, Sanjay is networking through friends and relatives and has 
come across investors from countries like Singapore and India, America and 
Turkey.<br>
<br>
Another US$63,000 is coming from the Nepal government’s kitty. The remaining 
amount has been collected through shares. People who are buying shares directly 
in this project will get the credit line as producers of the film. This is a 
unique way of getting funds for films, unprecedented in India. Once the film 
sells, the profit will be shared amongst these investors. Sanjay was in Muscat 
to collect the remaining 30 per cent fund for the movie. This is not his first 
visit. Earlier, Chanakyashastra, a play written and directed by him, received 
excellent response here in Muscat. This time round also, he was given a warm 
reception. <br>
<br>
Sanjay is very enthusiastic of the outcome. He says, “When you expose a film to 
the world market, the potential is unlimited. My film, which has kids climbing 
Mt Everest and has a reputed Hollywood agent as a distributor, will surely get a 
fairly good coverage in international media. All this put together, there is a 
strong possibility that the film is going to make good money.”<br>
<br>
When asked about his take on Indian cinema, he said, the success of Iqbal, Dor 
and Bheja Fry shows that<br>
India is now geared towards quality alternative cinema. 
That’s the future of Indian cinema, he says.<br>
<br>
<b>– Susmita De <br>
