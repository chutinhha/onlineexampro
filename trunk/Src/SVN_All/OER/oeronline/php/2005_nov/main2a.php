<p align="justify"><font face="Verdana" size="1"><b>How long have you been with 
Nokia in the Middle East market?</b><br>
I have been with Nokia for six years now in the Middle East. I started in 
marketing, then launched the multimedia business and eventually took over as the 
General Manager of the Middle East and North Africa office two years ago. <br>
<br>
<b>How is Nokia doing globally?</b><br>
We are doing very well. We regained market share of up to 34 per cent, and just 
marked our billionth sale recently in Nigeria. Our third quarter results were 
announced recently and we are generally pleased with our global performance.<br>
<b><br>
With people changing their mobile phones regularly, how do you ensure that they 
stay with Nokia? </b><br>
The figures change from market to market, country to country. I cannot give you 
specific statistics on what the replacement rates are. People tend to stay with 
Nokia for several reasons amongst which is the equity of the brand and other 
tangible reasons such as ease-of-use and that forms brand loyalty. <br>
<br>
The brand is of great value itself, as the majority of people using Nokia are 
actually proud of carrying the brand Nokia. We have worked hard on developing 
the brand over the last ten years in the region and it really has impacted our 
business positively. <br>
<br>
<b>Nokia is primarily a mobile communications company, but the mobile devices 
are turning more and more to entertainment features now. How are the features 
going to be changed in the future and what role will you play?<br>
</b><br>
We might be viewed as being a mobile phone company, but we believe to be in the 
mobility business. We are mobilizing the end user. <br>
<br>
Nokia is playing a huge role in mobilizing the entertainment business, the 
gaming business and enterprise and communication. Some years ago, you heard buzz 
words such as convergence. Then some eight years ago we talked about mobilizing 
email. Everybody thought we were mad. <br>
<br>
Today I walk around checking my emails on my communicator, my 9300 or any series 
60 phone with the same ease as on my PC. We underwent a restructuring two years 
ago, with one of our business groups converted into an independent multimedia 
unit and another for enterprise solutions, besides our mobile phone business. 
All the business groups together are delivering a portfolio of 40-50 phones to 
the end-users, each offering different aspects, depending on the requirements of 
the end-user.<br>
<br>
Some customers need more memory, others pitch for more gaming facilities, MP3 
facilities, or have detailed media consumption requirements. You name it and all 
of this is converging in the phones, all under the umbrella of mobility. The 
features are there as a platform, as a means to actually mobilize what you need. 
The possibilities are endless.<br>
<br>
<b>If you are putting your phones into three categories: higher, middle and lower 
value end models, how much are these categories contributing to the business and 
how do you see them changing over the next few years? </b> <br>
We segment the market in different ways, some are pricing-based, others are 
feature-based or consumer usage-based. If you are looking at a portfolio of 
40-plus products, it is impossible to say exactly where the lower end starts and 
ends. <br>
<br>
But when you look at the device structure from a feature set point of view, then 
it becomes clearer, as obviously, the highest value comes from the higher-end 
phone. <br>
<br>
To answer the second part of your question, demand drives change. Two years ago, 
the demand was for black-and-white phones. When colour phones were launched, 
they were initially positioned at the high end, but the steady demand for the 
product has made them more affordable over a period of time. That is a 
feature-driven product. It does not mean that there is a low or high end. <br>
But we are very conscious of not loading features into phones within price 
segments where they are not required, as that would simply increase the price. 
We do a lot of research to try and judge what feature sets are required and 
where. <br>
<b><br>
What new products do you see evolving over the next few years?</b><br>
You are going to see our music phone at the end of the year. The 4 gigabyte N91 
will be launched in late December/ early January, and a lot of other phones with 
a mix-and-match set of features are also on the anvil. <br>
<br>
The N90 has just been released, which is another imaging phone with a high 
number of mega pixels. Increasingly, phones are becoming more feature-focused, 
with all the phones having all the features to some extent, but some phones will 
be focusing on certain enhanced features. They will be optimized for specific 
usage. <br>
<br>
<b>Will you be developing different kinds of feature sets for the Middle East in 
particular?</b><br>
We do a lot of localization, such as the menus, the Arabic, the Hindi, or 
Persian language sets. Those are obvious and easy localizations. <br>
<br>
We recently started offering the Islamic calendar on phones, and we launched the 
Kibla direction, which are specialized applications directed at one specific 
market. We have a lot of software, which can be loaded as and when the customer 
decides. <br>
<br>
<b>Do you have any unique feature for the Middle East market, which your 
competitors don’t have?</b><br>
Most feature sets are available in the majority of devices. What is important is 
how to time their marketing and anticipate their demand. Having, for instance, 
Arabic language sets on the phones is not a privilege, it is a necessity 
nowadays. <br>
<br>
So everybody must offer it in this part of the world. It is not who offers what, 
but when and where you market it and who is the first to market a new feature.<br>
<br>
<b>How are you dealing with the challenges imposed by foreign companies such as 
LG and Samsung, who have brought out a large number of phones and are investing 
a lot of money to capture a slice of the market, especially in Asia? </b><br>
The challenge has been met. We are clearly number one. Every single market 
offers a challenge, no matter whether the company is Korean, European or 
American, whether they are a new provider of mobile phones or an established 
competitor. <br>
<br>
We see everybody as an equal competitor. We cannot afford to underestimate any 
player, but have to face each of them. <br>
<br>
<b>What has been the role of advertising in creating the brand Nokia?</b><br>
The role of advertising is crucial in business. The world’s best-kept secrets 
remain the world’s best-kept secrets if you don’t tell people about them. If you 
don’t make it public, you are not going to go anywhere. <br>
<br>
The brand today is one of our biggest assets. It is not the feature sets only, 
or the user interface only, but it is the brand, your emotional connection to 
the brand, your physical connection to the brand, and your aspirations to the 
brand and that did not come cheap, not only in monetary terms, but also with 
regard to the hard work and time spent. <br>
<br>
In this part of the world, where Nokia enjoys a very high equity, the brand 
recognition has not come easy. But in the end, it is the user who decides which 
brand to buy. We are happy that a majority of them choose Nokia and will 
continue working even harder to ensure that this sustains for the future too. </font></p>
