</font><font face="Verdana" size="1"><b>Fincorp looks at the Muscat Securities 
Market (MSM) which continues to fall after a historic high in June</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt"> 
T</font><font face="Verdana" size="1">he markets continued to witness a fall 
during the period under review (21 August – 22 September 2005) with very little 
activity observed in shares of all the companies except Omantel. The Omantel 
scrip alone accounted for 51 per cent of the total volumes of RO59.6 million 
recorded for the period under review. The correction during the period brought 
the Index down by 132 points to close at 5,019 levels and losing more than 700 
points from its all time peak of 5699 reached during June 2005. Overall, the 
year to date gain is still at a healthy 49 per cent for the Index which is 
incidentally the highest when the gains registered for the years 2002, 2003 and 
2004 are compared. <br>
<br>
<b>Vacations for the markets too</b><br>
It was all about Omantel trading during the last month which occupied limelight 
upon its listing and the glory proved to be short lived. Though Omantel counter 
was responsible for more than 50 per cent of the trading during the current 
review period, the volumes per day for the market as a whole came down sharply 
to levels of RO2.48 million from a level of RO5.47 million per day observed till 
July 2005. In fact, the volume of trade that took place in the period under 
review perhaps is the lowest in the current year revealing the dullness 
associated with the trading activity and confirming the effect of vacation 
season on the markets. Further markets have already discounted the good results 
declared by the corporates for the first half of 2005 and in all probability 
adjusted the valuations for the revised earnings.</font></p>
