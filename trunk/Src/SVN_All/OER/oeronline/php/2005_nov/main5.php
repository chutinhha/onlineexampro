<p align="justify" dir="ltr"><font face="Verdana" size="1"> 
<b>Market mystery</b><br>
There was trading activity only in respect of 74 counters during the period 
under review as against the total 124 companies listed on MSM which is in line 
with the trend seen during the past. Out of the 74 counters, only 24 counters 
gained with the rest of the 50 counters losing value thereby ending the period 
with advance/decliners ratio of 1:2. As a change, Al Amal Income Fund stole the 
limelight and topped the gainers list with a positive return of 22 per cent 
ending the period with a price of RO1.22. Al Khaleej Polypropylene Industries 
followed with a gain of 11 per cent. Amongst the losers, Majan College lost a 
whopping RO3.410 to end the period with RO7.14 which could be due to the 73 per 
cent weightage accounted for by the fair value gains of its investments in the 
net profit of RO1.11 million for the nine months period ended as on 31 May, 
2005. Is it fair enough for the corporates to depend to a large extent on fair 
value gains to consolidate their bottom line and will they be faring all right 
without the fair value gains is a fairy tale to be analyzed. An attempt has been 
made here to decipher this myth and find out the improvement in the earnings of 
corporates if any without the fair value gains from their half yearly results.<br>
<br>
<b>Services Sector</b><br>
The least performing sector on the Index has been the services sector which has 
seen an appreciation of just 86 per cent, since the nadir reached by the market 
during December 2001 as against a whopping 243 per cent by the Index. The 
Services sector makes an interesting case to know whether the investors have 
rightly judged its performance. <br>
<br>
<b>Industry Sector</b><br>
Being the largest gainer for 2004, and hence, the most observed sector in recent 
months amongst investors, the Industry sector continues to give above market 
return gains in the current year. However, it has slightly fallen back the 
leader on the board for 2005 viz. Banking &amp; Investment sector with the total 
returns as of date at around 50 per cent as against 53 per cent for the largest 
market cap. Sector. The acceleration in the share prices have still been 
maintained inspite of stagnation setting in the earnings growth. In fact, the 
increase in Index gets magnified if one compares it with the earnings at the 
operating level which has increased by just 24 per cent, while the share prices 
represented by the Industry Index has grown by more than double that figure.<br>
<br>
<b>Banking &amp; Investment Sector</b><br>
The Banking and the Investment sector constitute more than 65 per cent of the 
total MSM Index and hence, dictate the movement of the Index itself. However, 
the companies representing this sector are very diverse, as it not only contains 
pure banking institutions, but also investment &amp; holding companies. As against 
an annualized fair value gains (realized &amp; unrealized) of just RO15 million, the 
overall fair value gains in investments have already crossed RO75 million by 
June 2005. <br>
<br>
The profitability figures in the current year has further inflated as compared 
to the 2004 results due to the reduction in the provisions which plagued the 
Banking Sector during that time. More specifically, due to National Bank of Oman 
and Bank Muscat, which normally increase their provisions during the ending 
quarters. <br>
<br>
<b>Outlook</b><br>
We have seen the liquidity getting sucked up during and after the Omantel issue. 
The situation might continue for some more time. There could be various factors 
behind it. High Networth Individuals who have been quite active on the Omani 
markets for the past few months have taken a back seat because of their 
secondary holdings, which have taken a whack due to the MSM Index falling by 
more than 10 per cent. Retail investors who have not been able to liquidate 
their Omantel stock due to their targeted prices are also not willing to take 
extra risks to enter, even into quality earning stocks. Traders who normally 
play a key role are also not been able to create liquidity, as the selling 
pressure has been constant.</font></p>
