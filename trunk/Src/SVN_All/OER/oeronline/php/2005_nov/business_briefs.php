                    in 2004. <br>
                    <br>
                    The bank’s net worth has increased to RO 157m during the 
                    quarter due to the private placement of 10m shares to 
                    Commercial Bank of Qatar in July 2005 at a total price of RO 
                    4.450 per share. The market capitalization has also 
                    increased from RO 171m in December 2004 to RO 320m on 30 
                    September 2005. <br>
                    <br>
                    Based on the increase in capital, the bank has strengthened 
                    its capital adequacy position and now has a robust BIS 
                    capital adequacy ratio of 25.67 per cent as at September 
                    2005, significantly higher than the minimum 12 per cent 
                    level stipulated by the Central Bank of Oman.<br>
                    <br>
                    Provision coverage has continued to improve moving up from 
                    81.5 per cent as at the end of 2004 to 84.6 per cent as on 
                    30 September 2005, demonstrating the improved strength of 
                    the bank. Operating costs of RO 14.6m for the period ended 
                    30 September 2005 was lower by RO 0.5 million (3 per cent) 
                    than the RO 15.1m charge recorded for the same period in 
                    2004.</font></p>
