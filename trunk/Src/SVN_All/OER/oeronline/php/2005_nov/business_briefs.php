                        <b>Arab Economic Awards<br>
                    </b><br>
                    The International Research Foundation (IRF), in association 
                    with the Fraser Institute (Canada), announced the launch of 
                    the first Arab Economic Freedom Awards. The awards, to be 
                    given this year, celebrate the achievements of a government 
                    in the creation of wealth through the promotion of economic 
                    freedom, said Fred McMahon, director of the Centre for 
                    Globalisation Studies at the Fraser Institute. <br>
                    <br>
                    IRF is a 
                    non-governmental and non-profitable company based in Oman. 
                    The vision of the company is creation of wealth and jobs by 
                    promoting economic freedom. IRF will have an Advisory Board 
                    representing professionals and eminent personalities of Oman 
                    to advise on the research agenda. The company has signed a 
                    memorandum of understanding with the Fraser Institute for 
                    co-operation. <br>
                    <br>
                    The Arab Economic Freedom Awards are integral to the Arab 
                    Freedom Index that is being produced by the Fraser Institute 
                    and its regional partner in Oman — the International 
                    Research Foundation. The new initiative will measure the 
                    accomplishments of governments of 16 Arab countries in 
                    embracing free market policies.<br>
                    <br>
                    The Arab Economic Freedom Awards will be awarded for 
                    achievements in the five identified parameters that 
                    constitute the measure of economic freedom. The awards that 
                    have been instituted are the Lean Government Award 2005, 
                    Rule of Law Award 2005, Sound Money Award 2005, Free Trade 
                    Award 2005 and Ease of Business Award 2005. A sixth award 
                    will also be awarded for the country that ranks the highest 
                    in all the identified parameters. The awards will be 
                    announced at a gala dinner on November 20 at Al Bustan 
                    Palace Hotel.<br>
                    Officials from the Omani Centre for Investment Promotion &amp; 
                    Export Development among others comprise the organising 
                    committee for the event. 
