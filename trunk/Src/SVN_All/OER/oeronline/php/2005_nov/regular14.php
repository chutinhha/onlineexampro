<b>Omantel</b><br>
Omantel counter will be opened for entry to the regional investors as well as to 
the domestic funds/ companies / institutions from the 27th of October 2005. As 
against the projected net profit of RO 75 mn for the year 2005, omantel has 
achieved a net profit of RO 39.1 mn for the half year 2005. It makes an 
interersting reading to know how the telecom company shares are quoting in the 
GCC markets for us to predict (rightly or erroneously) the range within which 
Omantel shares may quote. The following table will be helpful to decipher the 
intrinsic strength of omantel shares upon its opening to the regional investors 
/ domestic institutions and companies.<br>
<br>
It can be seen that out of the 5 markets, telecom shares are quoting at a 
discount to their respective market PEs namely in Saudi Arabia, Qatar and UAE 
whereas in the remaining 2 markets, the telecom shares are quoting at a premium 
namely in Kuwait and Bahrain. <br>
<br>
Oman automatically falls under the latter group category and it can be safely 
asumed that Omantel will be quoting at a premium to that of MSM market. But the 
question remains to what extent. <br>
<br>
