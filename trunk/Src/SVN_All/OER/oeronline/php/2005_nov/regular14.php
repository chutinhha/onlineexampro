30% to 35% to that of the market. Again assuming that the market will have a P/E 
of 19 at the time of entry of these investors, it brings us to a price range of 
RO 2.5 to 2.6. However on the book value count, the stock has very good 
potential to appreciate even further. Investors are accordingly advised to 
position themselves in the market for optimizing returns from Omantel shares 
counter.<br>
