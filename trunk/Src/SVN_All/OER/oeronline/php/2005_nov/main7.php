<p align="justify" dir="ltr"><font face="Verdana" size="1">
<b>The Puegeot 407, a new entrant to the executive sedan segment, comes across 
as a worthy successor to the 406</b></font></p>
<p align="center" dir="ltr">
<font face="Verdana" size="3"><b>
<img border="2" src="carr1.jpg" width="320" height="159"></b></font></p>

<p align="justify" dir="ltr">
<font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1"> have 
been using personal computers for about 12 years now. Earlier, I had a Pentium 
100, with 32 Megabytes of memory and a 1.2GB hard drive. Now, I run a Pentium 4 
+ with 512MB of RAM and a 40GB hard drive. In automotive terms, this would be 
the equivalent of upgrading from a Suzuki Alto to a Porsche Carrera GT. And come 
to think about it, I used to get more or less the same things done with my old 
PC as I do with the new one.<br>
<br>
Sure, the Pentium 4+ is about a zillion times faster than the old PC, but more 
often than not, it only gets used for logging on to the Internet, writing 
emails, chatting with friends, messing around a bit with images shot on a 
digital camera etc. Sometimes, I think it is just one big conspiracy, hatched to 
keep us hooked to our credit cards and keep PC/Gizmo manufacturers minting 
money. We end up buying bazookas when all we need, perhaps, is a shotgun. Which 
is where the new Peugeot 407 comes in - it gives you just enough.<br>
<br>
Peugeot brings in a new entrant into the compact executive sedan segment with 
its all-new 407. It is well equipped with features, striking looks and has an 
extremely competitive pricing. The 407 is a worthy successor to the 406, which 
is a worthy vehicle and did a lot to change Peugeot’s image, but the 407 is 
better in every aspect!<br>
<br>
It is well groomed, spacious and offers loads of standard equipment. Moreover, 
there is a choice of three very competent petrol engines. Although some might 
like to compare it against cars such as the 3 Series, C-Class and A4, it is 
actually a total different kind of vehicle.</font></p>

<p align="justify" dir="ltr">
<font face="Verdana" size="1">These segment cars across the world are seeing a 
fair amount of competition. It has got contenders like the Ford Mondeo, VW 
Passat, Honda Accord, Mazda6 and the Toyota Camry. However, the Peugeot’s 
distinctive design will make it stand out in a crowd. <br>
<br>
The new Peugeot’s most prominent design feature is the big front air intake, 
which makes it, seem as if the 407 is ready to gobble all competitors for 
breakfast. Curvy body styling, gorgeously sleek lines and big glass areas 
compliment the car’s prominent nose design. The car actually appears super 
expensive.<br>
<br>
A road test will confirm things, but on the face of it, the Peugeot seems to be 
in a good position to win the battle of the spec sheets.<br>
<br>
Initially I approached the car with some trepidation - the Peugeot 1997 cc 
engine boasts of all the right bits (DOHC, 16V, 85 mm cylinder bore, Cornering 
Brake Control, Dynamic Stability Control, Cruise Control…}. <br>
<br>
Now, I wouldn’t call the Peugeot sluggish, but it isn’t exactly bursting with 
verve either, so the idea of a smaller-engined Peugeot didn't seem extremely 
appealing to me. Then again, the Honda City makes do with all of 77 horses, so 
how bad could the Peugeot 2.0 be? As it turned out, not bad at all! In fact, the 
2.0 has no driveability issues whatsoever - it can hold its own in traffic or on 
the highway, and is as happy chugging along at 50 kph as at 120 kph.<br>
<br>
If you are a devoted City VTEC fiend, you won't find happiness here, but others 
shouldn't be disappointed. Though this was not a full-on test, I pitted the 
Peugeot against the trusty stopwatch anyway, and the car didn’t do too badly at 
all; 0 to 62 mph comes up in 9.1 seconds, which is not bad at all.<br>
<br>
More than engine performance, Peugeot has also given a lot of attention to the 
407’s build quality, and there is no doubt the company has succeeded. The 
interior plastics are made of high-quality material and the overall dashboard 
design is attractive. Ergonomics are good, while standard on all 407s are 
fingertip controls for the audio system as well as cruise control. <br>
<br>
Standard features on all 407s include climate 
control with dual settings, automatic headlights, automatic windscreen wipers 
with rain sensors, tyre-pressure sensors and electric foldaway side mirrors. 
Sitting in this car feels good - the cabin is nice and plush and most controls 
are laid out in a logical and ergonomic manner. That's not to say the entire 
interior is up to Audi standards, but the initial impression is that this 
Peugeot cabin is definitely on par with its direct competitors. <br>
<br>
The 2.2 and 3.0 models get electrically operated front seats and rear park 
distance control. High-quality cloth trim is standard on the entry-level 2-litre 
407, while the 2.2 model gets a combination of leather and cloth trim. The 
range-topper has full leather trim. The 407, which has scored five stars in the 
highly-acclaimed Euro NCAP crash tests, has a host of airbags including front 
driver and passenger airbags, a steering column airbag, side airbags in the 
seats, and side curtain airbags. <br>
<br>
Everything on the car seems to belong there, in a nice, wholesome, 
value-for-money family-saloon and it looks cool when a woman drives it… The 
entry-level 2-litre 407 with 100kW and 190Nm is not the sportiest kid on the 
block, but it offers acceptable power and makes a good cruiser. Power goes to 
the front wheels through either a five-speed manual or four-speed automatic 
transmission.<br>
<br>
The top of the range 3-litre V6 has a power output of 155 kW and torque is a 
hefty 290 Nm, while it comes with a new six-speed automatic gearbox. Of course 
it is a willing performer with load of power on hand, but it is heavier than the 
2.2-litre.<br>
<br>
Along with the 407 station wagon, Peugeot has a 2-litre diesel model. Although 
it will be a difficult task for Peugeot, (as it is for Honda, Mazda, Ford and 
Toyota) to convince image conscious buyers to opt for 407, instead of cars which 
come from manufacturers such as the BMW and Mercedes.<br>
<br>
Stopping power comes from four large discs, with the front two ventilated. It 
felt a tad soggy, but maybe I'm just hypercritical. All three 407 models have 
ABS with electronic brake distribution for each wheel. In addition, there are 
also emergency braking assistance, and ESP. <br>
<br>
Adequate power, generous amounts of cabin and boot space, nice fit and finish 
and fair price - undeniably, the Peugeot seems to be a sensible deal. Peugeot 
should also throw in some freebies, other than the normal free services to push 
up the car sales.<br>
<br>
The 407 is a showcase of good driving dynamics, offering a supple ride with 
sharp handling. The suspension setup results in very high quality directional 
road holding under all circumstances, even on rough and deeply rutted roads, and 
it consists of a dual wishbone front axle with drop link, and an integral 
multi-arm rear suspension with inclined spring and damper assemblies. The 
Peugeot is a kind of road-racing champ, but it does have a combination of 
somewhat firm brakes, a powerful engine and predictable handling that makes it 
an entertaining drive, should the mood strike you.<br>
<br>
With the Peugeot's excellent attention to detail and generous standard features, 
the price is just icing on the cake. It may sound like blasphemy, but Camry and 
Accord shoppers now have one more stop to make at the local auto mall before 
deciding on a midsize sedan. And now, let's see if I can make that 512MB of RAM 
on my PC work for a living. </font></p>

