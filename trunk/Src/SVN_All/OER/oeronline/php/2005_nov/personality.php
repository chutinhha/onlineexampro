</b></font><b><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="3">S</font></b><font face="Verdana" size="1">uccess 
rests easily on Basil Macki. At 33, he is the Managing Director of leading Omani 
family-run construction services company, Special Technical Services. But he 
believes it is too early for him to talk about the feat as an achievement.<br>
<br>
Macki laughingly dismisses off a suggestion that he has already acquired the 
wisdom of a veteran business leader. “Not at all, I’ve hardly started yet. I’m 
still a novice in the field. I’m only 33.”<br>
<br>
Macki says that long years of experience were essential to maintaining a great 
business and deriving fulfillment out of the achievement. His vision is clear: 
to grow the family-run company’s construction and fabrication services to the 
pinnacle. And there are no shortcuts as he believes it can only be achieved 
through “hard work, dedication and being in touch with your working partners.”<br>
<br>
<b>Early years</b><br>
Macki was born in Scotland, from an Omani father and a Scottish mother. After 
finishing junior school in Muscat, he was sent to a boarding school in the UK at 
the age of 11. He completed his university studies by obtaining a Masters in 
Mechanical Engineering from Imperial College, University of London.<br>
<br>
He returned to Oman to work for Petroleum Development of Oman (PDO) which had 
financed his studies through a scholarship. But the work stint with PDO lasted 
for only a year after which he was cross posted within the Shell system, to 
Holland where he worked for two years. He left the job to pursue successfully a 
Masters in Business Administration from the Dutch “Nyenrode University”.<br>
<br>
That was the beginning of his business career. He worked with Oman Oilfield 
Supply Centre from October 2000 to February 2003 and then joined his current 
business. “I started supervising contractors in PDO and ended up being a 
contractor myself,” Macki says, with a twinkle in his eyes.<br>
<br>
He says the first thing he learned was how to work in a team and achieve common 
goals. “This does not give you much space for private maneuvering, but on the 
other hand you have plenty of resources and several minds that work together to 
enhance the group’s achievements.”<br>
<br>
<b>
<img border="0" src="pers1.jpg" align="left" hspace="5" vspace="2" width="154" height="148">Future 
goals and challenges</b><br>
Macki says the scope for achieving such goals in the oil and gas/petrochemical 
infrastructure is immense within Oman’s Sohar region itself. “If you asked me a 
few years ago, I would’ve said ‘Yes, we want to expand outside the country, we 
want to be in the Gulf, in other regions.’ But we don’t want that now. We think 
there are so many opportunities in Oman. So, if we go outside and split 
ourselves up, we’ll be just doing so many things, but not doing them well. 
Maybe, in the longer term, but not in the near future (venturing outside of 
Oman)! There’s so much to do in Sohar. It needs to be built, to set up its 
infrastructure, to be maintained...”<br>
<br>
Adds Macki: “There is an immense infrastructure that needs to be developed in 
the Sohar region, attracting all kinds of related industrial activities. The 
workforce that is being settled there and in other regions of the country 
provide a large market that is growing steadily.”<br>
<br>
The first goal is to become stronger within Oman’s shores. “To become a highly 
respected and wholly-invested contracting company. We want to be a 
well-established company, to expand and become stronger in Oman, first.”<br>
<br>
And he says there are many, many challenges to crossing this milestone. “Right 
now, for example, our major challenge is pinpointing exactly what areas of Sohar 
development we would like to concentrate” he says.<br>
<br>
“We must think of preserving our reputation as a first choice company that 
delivers good quality work. In two years time, there will be plenty of jobs 
offered, but we need to be in the position where we are successful in being 
awarded the ones that best suite our company size, structure and experience.”<br>
<br>
Macki says he had a hard time making the shift initially in the nature of his 
work -- from working as a process and construction engineer within a major 
Hydrocarbon operating company to the head of a family business. “I was facing 
less engineering challenges and more management tasks, which included 
controlling the company’s accountability, ensuring profitability at the end of 
the month and many other daily nuisances.”<br>
<br>
<b>Region's scenario </b><br>
Macki says STS was not only in sight of its own goals, but also the targets for 
Omanisation set by the government. “We have already gone a long way on this 
project. Today, we’ve got 35 per cent of positions Omanised. But we are focusing 
more and more on attracting skilled and experienced nationals instead of filling 
the quotas.”<br>
<br>
Sixty per cent of STS workforce will be Omanised by 2007, an enormous challenge 
for a company that believes maintaining their quality standard is sacred. It 
means the company is sparing no effort in scouring for the right kind of 
personnel and hiring only after an intense screening. STS officials go to 
colleges to interview potential employees. They invest in training once they 
have identified such a candidate. And preference is given to skilled and 
experienced personnel for senior positions.<br>
<br>
“It is difficult to find the right-skilled Omani people for the right positions, 
so the company goes into the process of training to ensure their staff are 
highly qualified to carry out their tasks. Any Omani applying for a job in our 
company has to go through a series of interviews before they are selected,” he 
informs. Macki adds that the objective of selecting and training school 
pass-outs was to meet the target of Omanisation by 2007. <br>
<br>
STS, a member of Oman Petroleum Alliance (OPAL) has received a Compliance 
Verification Certificate, which is awarded to companies that have promised to 
make a certain percentage (for STS this target is 60 per cent) of their 
workforce Omani nationals by 2007, among other OPAL guidelines.<br>
<br>
“I believe it’s the best period of time to develop the country and improve the 
growth of the national economy. My first years here were very tough, but now 
things are getting much better, and I expect a boom in the major sectors of the 
Omani economy, especially in the Oil and Gas industry (both upstream and 
downstream), real estate and in tourism,” Macki asserts, adding that although 
STS was focusing only on industrial based service, other family-owned businesses 
are engaging in opportunities in other sectors.<br>
<br>
<b>Family life</b><br>
Macki lives on his own in Madinat Qaboos. Leisure time during weekends is spent 
mostly off the coast of Oman, as he very much enjoys sea-based activities. He 
loves to “practise water-skiing, enjoys having barbecues on boats or on the 
beach with my friends.”<br>
<br>
Macki’s life is governed by straightforward values. “With honesty, you can do 
everything. At home or at work, you can only deal with people in the same way.” </font></p>

