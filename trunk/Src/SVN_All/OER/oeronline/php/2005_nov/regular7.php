<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>P</b></font><font face="Verdana" size="1">eople 
read by recognising word-shapes, not individual letters. Don’t use upper case 
(capital letters) for text and ideally not for headlines either, as it takes 
longer to read and reduces impact.<br>
<br>
<b>Stick it There</b><img border="0" src="marketi.jpg" align="right" width="195" height="194"><br>
Position your headline statement where it can be seen quickest. Don’t put 
headlines at the very top of the space. The eye is naturally drawn to between 
two-thirds and three-quarters up the page or space, which is where the main 
benefit statement needs to be.<br>
<br>
<b>Press Releases</b><br>
Newspapers need news submitted by the local business community or they have to 
pay more for journalists to go out and get news. Look through Oman Daily 
Observer, Oman Tribune, Times of Oman, Shabiba, Al Watan and Oman to spot the PR 
material submitted by commercial organisations. This should encourage you as to 
how easy it is to provide news stories for the press.<br>
<br>
<b>Photos Improve Editorial Take-up by 100%</b><br>
A good photograph in support of a press release will dramatically improve your 
chance of publication. Either provide your own, or if the story is an event, ask 
the press to send their photographers. <br>
<br>
<b>Do it Now!</b><br>
If you’ve got something newsworthy, don’t wait or else the opportunity will be 
lost. Even simple things such as staff promotions, news on presentations 
delivered at conferences, all make acceptable PR stories. Always be on the 
lookout for the quirky and unusual.<br>
<br>
<b>Editorial Coverage versus Display Advertising </b><br>
If you plan to pay for display advertising or inserts in any type of 
publication, always ask before giving the order if you can have some editorial 
coverage as a condition for placing the advertising business. Many publications 
will agree to this and you’ll have some free editorial to support the advert. 
Some publications combine the two and sell ‘advertorial’ feature space, which 
purports to be news, but is really a large paid-for advert.<br>
<br>
<b>Directories</b> <br>
Directories are often good ways to generate enquiries for consumer businesses, 
but are not appropriate for all types of B2B and B2G sectors. Ask yourself: 
where would my potential customers look for my product or service?<br>
<br>
<b>Directories – Internet</b><br>
Internet directories and specialist search engines are an increasingly effective 
way to advertise and market services and products, because so many customers now 
use these listings to find suppliers. Many listings are free. Some work well, 
others don’t. Many listings aren’t free. Again, some work well and others don’t.
<br>
<br>
<b>Brochures &amp; Leaflets</b><br>
Brochures and leaflets can be used for a variety of purposes and can be 
distributed in a variety of ways. A good printer can provide examples and 
costing, and the easiest way to learn what works and what doesn’t, is to look at 
other people’s material. <br>
<br>
The purpose of a brochure is first and foremost to generate new business through 
providing information in a way that appeals to the reader. The acronym AIDA (as 
discussed earlier) should be the basis of its design. Some brochures and 
leaflets are pleasing pieces of art, but they don’t achieve anything for the 
business, so avoid falling into this trap. If you work with a designer (and you 
should), be sure to control any fanciful tendencies and keep the message and 
style to the point. <br>
<br>
When producing corporate material, think about how they’ll be distributed. If it 
needs an envelope, try to avoid using a non-standard envelope size, which will 
add cost unnecessarily. If the material is required as an insert, is it 
acceptable to the publication? Do you want people to retain the material? If so, 
perhaps a business card or plastic credit-card-type attachment can help. <br>
<br>
<b>Loose &amp; Bound </b><br>
Inserts, in the form of leaflets, brochures, or other material, are provided by 
the advertiser to the publication, to be sent out with the magazine or 
newspaper. You have to produce the materials to be used as inserts, which incurs 
printing costs and then paying the publication a charge for insertion. There’s a 
big effect from the economies of scale. <br>
<br>
Charges vary according to weight of insert: how many inserts per publication, 
volume, the narrowness of the circulation profile and how the publication is 
itself distributed. Response rates from inserts are generally lower than direct 
mail, but inserts are a flexible and relatively cheaper method of distributing 
an advert to a target audience. Bound-in inserts cost extra, require longer 
lead-times and are favoured by some advertisers because they don’t fall out and 
consequently, are seen by more of the total readership, which can be two or 
three times greater than the circulation.<br>
<br>
Posters Sites (hoardings, hotels, airports, shopping centres, supermarkets &amp; 
buses)&nbsp; Advertising sites vary according to nature and cost, from large 
highway billboards to buses, supermarkets, petrol stations, hotels, airports and 
sports grounds. Anywhere that people pass or gather in large numbers is a 
potential poster site, and as with the print media, audience profile information 
is usually available. New sites are being discovered and exploited all the time, 
such as supermarket trolleys, floors and table napkins. <br>
<br>
<b>Speak Up</b><br>
If you can speak knowledgeably about your product/service and tie it in well 
with current events, like Raza Ashraf of Total Alignment did recently when he 
delivered a Business Continuity Planning Open House presentation at Knowledge 
Oasis Muscat (KOM). Very timely given the horrendous damage caused by Hurricane 
Katrina in New Orleans. Indeed, as Ernie Svenson, whose New Orleans firm is 
trying to get back on its feet, said: &quot;My advice to the [law] firms that haven't 
set up a disaster recovery plan is to start now.&quot;<br>
Radio interviews are also a great way to reach a wide audience and improve your 
customer base. <br>
<br>
<b>B2B &amp; B2G Organisations &amp; Web Presence</b><br>
Particular styles and originality are required for a good website and the medium 
is no longer passive, so you need to think about integrating promotion and 
advertising to attract people to your site. <br>
<br>
<b>Open Days &amp; Exhibitions</b><br>
The advantage of personal contact is that you actually get to talk to your 
potential customers, which dramatically increases the chances of getting your 
message across. But there is a limit to how many people you can target and 
access using these methods. Costs of preparation and organisation can be large 
and are rarely transparent at the outset. So beware! <br>
<br>
Events of this nature do nevertheless offer excellent possibilities for 
follow-up PR activity, which can contribute greatly to building a 
customer-friendly image. Just look at what KOM has achieved over the past two 
years through its monthly Open House seminar program. It’s worked so well that 
it has attracted the support of OER, Oman Mobile, Omantel, Oman Air, BankMuscat 
and OEPNPA – these organizations obviously see benefit in associating themselves 
with a monthly ‘thinking-man’s’ business event.<br>
<br>
<b>Word-of-Mouth</b><br>
Personal referral is unsurpassed as an advertising tool. It costs nothing and is 
the most believable of all. This is therefore a positive reason for sustaining 
excellent customer service and relations. If your customers are thrilled by the 
service you give, they’ll tell their friends and colleagues. Use these referrals 
in brochures, on the website and in your press releases.<br>
<br>
<b>Networking &amp; Clubs</b><br>
Networking contacts and introductions is an especially cost-effective marketing 
method for B2B services. A variety of networking opportunities exist, including 
Open House seminars, chambers of commerce and trade, networking websites, clubs, 
breakfasts, lunches, and anywhere that potential customers and influencers 
gather, and the systems within which they communicate and socialise. <br>
<br>
<b>Voicemail </b><br>
Even the outgoing message on your answering machine or voicemail system can help 
promote your business when utilized correctly. Make sure your message provides 
additional product or service information so that it’s working for you even at 
midnight. <br>
<br>
<b>Case Studies</b><br>
Case studies are not just for scientific applications. Studies of your service/ 
product(s) in action can help your potential customers learn more and make an 
educated decision before they buy. <br>
<br>
The areas covered in this two-part article ought to help you piece together a 
better and more profitable ad campaign. Indeed, advertising plays an important 
role in our economy. When executed well, its images and messages fill our lives, 
its brilliance – on a great day – stimulates our minds and its success leaves a 
hole in our pocket. </font></p>

