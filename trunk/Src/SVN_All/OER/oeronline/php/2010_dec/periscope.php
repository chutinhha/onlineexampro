                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2">The British pound, 
                    the Indian rupee and the Singapore dollar are strong 
                    currency picks for 2011</font></b><font face="Verdana" size="1"><br>
                    <br>
                    <br>
                    <img border="0" src="images/peri.jpg" align="right" hspace="12" width="150" height="239">The G-20 conclave in Seoul, to put it bluntly, was a 
                    failure. It is imperative that investors in the GCC protect 
                    their wealth by investing in currencies that have the 
                    potential to move higher in 2011. The Euro can no longer 
                    play the role of a liquid, anti-dollar reserve currency 
                    because the Irish, Greek and Portuguese sovereign debt 
                    crisis could well end up in a restructuring that will shred 
                    the credibility of the ECB and the EU. My ‘strong currency’ 
                    picks for 2011 are the British pound, the Indian rupee and 
                    the Singapore dollar. Why?<br>
                    <br>
                    Sterling’s value in the currency markets is determined by 
                    two divergent macro forces. While it is cheap on purchasing 
                    power criteria to the Euro, Chancellor Osborne’s most 
                    draconian austerity budget since the Thatcher era at a time 
                    when the Bank of England’s near zero base rate guarantees 
                    easy money is not exactly a formula for a stronger pound. 
                    However, it is now clear that Governor King and his merry 
                    knights on the central bank’s Monetary Policy Council (MPC) 
                    have grossly underestimated both economic growth and 
                    inflation, which is at least a full point above the Old Lady 
                    of Threadneedle Street’s 2 per cent target. UK economic 
                    growth could well surprise on the upside in 2011, to as high 
                    as 2.5 per cent, as the latest UK services PMI suggests to 
                    VAT, the spiral in food and energy prices, higher priced 
                    High Street goods and the epic sterling devaluation of 2008, 
                    which even exceeded the free fall after the Black Wednesday 
                    – ERM crisis in September 1992.<br>
                    <br>
                    This means an expansion of the Bank of England’s existing 
                    200bn pound quant easing programming is unthinkable. In 
                    fact, I strongly believe that the Bank of England will be 
                    the first major Western central bank to begin hiking its 0.5 
                    per cent base rate, possibly as early as in summer 2011, 
                    even though governor King is reluctant to tighten 
                    prematurely given the magnitude of government spending cuts.<br>
                    <br>
                    A major sterling move against the Euro is inevitable as UK 
                    growth eclipses Eurozone growth while the risk of a Irish, 
                    Portuguese and Greek sovereign debt restructuring where 
                    private investors take a painful cash haircut is inevitable. 
                    In any case, I see a rise in sterling to 1.85 against the 
                    dollar next year and a fall in Euro-sterling from the 
                    current 0.85 to as low as 0.78.<br>
                    <br>
                    <b>Look East</b><br>
                    Asian currencies have appreciated strongly in 2010, thanks 
                    to the Fed’s money printing spree, strong capital flows from 
                    the West, persistent trade surpluses and increasingly 
                    hawkish local central banks who must now combat a resurgence 
                    of inflation. Take India, for instance. Sensex 20,000 has 
                    attracted as a magnet for 1.2 trillion rupees in offshore 
                    hot money, a tidal wave of foreign speculative capital that 
                    has never been seen in the history of South Asia. A positive 
                    monsoon, robust consumption/ industrial production and 
                    strong bank credit growth all suggest that Indian GDP growth 
                    will hover at the 8.5 per cent level in 2011. This means 
                    wholesale inflation will continue to rise, particularly 
                    since the world commodity markets are white hot and crude 
                    oil has flirted  near $90.<br>
                    <br>
                    It is now inevitable that wholesale price inflation could 
                    rise to as high as 12 per cent next year and governor D 
                    Subbarao will have no choice but to accelerate the pace of 
                    Reserve Bank of India (RBI) monetary tightening in order to 
                    preempt a 1970 style inflation spiral. I would not be 
                    surprised to see the RBI tighten policy rates by 100 basis 
                    points by summer 2011 after a pause in December. This 
                    tightening will coincide with a reduction in the fiscal 
                    deficit as tax collections, 3G auction windfalls and the 
                    Congress’s divestment programme gaining traction. So while 
                    risk aversion in markets can weaken the Indian rupee 
                    temporarily as FII inflows reverse, I doubt if it will 
                    weaken much below 46. The central theme for 2011 is a rising 
                    rupee, possibly to as high as 42 against  the US dollar.<br>
                    <br>
                    <b>Singapore story</b><br>
                    Singapore’s GDP growth rate has been a phenomenal 15 per 
                    cent, the highest since the Lion City achieved independence 
                    from the Malayan Federation in August 1965. Singapore’s high 
                    tech electronics exports, role as the private banking hub of 
                    Southeast Asia, the phenomenal success of the two new 
                    casinos and stellar financial magnet have made the Singapore 
                    dollar the ideal safe haven currency for Asia’s multitude of 
                    affluent investors. The Monetary Authority of Singapore 
                    (MAS) has made it clear that it favours a higher Singapore 
                    dollar to combat a rise in domestic inflation and to act as 
                    a proxy for a rising Chinese yuan.<br>
                    <br>
                    The MAS wants to hedge the inflation risk Singapore faces 
                    due to the Federal Reserve’s ultra-loose monetary policies 
                    with a wider, higher trading band for the Singapore dollar. 
                    Singapore will also attract huge FDI now that it has signed 
                    Free Trade Agreements with China and ASEAN. Like the Swiss 
                    franc in the 1980s, the Singapore dollar is all set to 
                    become the world’s safe haven currency at a time when the 
                    Big Three central banks (Fed, ECB, Bank of Japan) are all 
                    determined to manipulate their currencies. I believe the 
                    Singapore dollar can rise to as high as 1.20 against the 
                    dollar in 2011 from the current 1.29 rate. </font></p>
