                    </b></font><font face="Verdana" size="1">
                    <b>have been developing corporate and specialised 
                    training programmes for the past ten years. How do you see 
                    the evolution of corporate coaching during this period?<br>
                    </b>As you know, the issues in the 21st century are very 
                    different from those in the 20th century. As industry 
                    developed, the systems needed simpler methods. Since the 
                    80s, technology has developed fast necessitating a different 
                    kind of working mind in our offices and work places. We also 
                    need different kinds of training and abilities focused on 
                    the future.<br>
                    <br>
                    The 21st century is the time when we are handling something 
                    which we had never dealt with before. The amount of change 
                    is phenomenal in the last ten years. When we consider twenty 
                    five major industries in the world from manufacturing to 
                    textiles to building and construction their requirement for 
                    technological change is very much in evidence. So our 
                    managers need to be cautious about the incredible stress. 
                    They have to learn how to manage this new technology. They 
                    also need to be able to stay relaxed, in touch with people 
                    and enjoy their job. Corporate coaching leaders need to 
                    follow an integrated coaching model developed for creating 
                    positive culture change.<br>
                    <br>
                    <b>You have been a dedicated student of Milton Erickson for 
                    years and gave his name to the college you founded. How 
                    ideal and effective is Ericksonian Communication for leaders 
                    and managers?</b><br>
                    It is based on the assumption that people are essentially 
                    all right. It does not matter how rigid or difficult the 
                    system they are into. They can be trained in all aspects of 
                    it. It takes three weeks for human beings to regain their 
                    balance and their flexibility. Once people understand this 
                    they relax about themselves and start to treat themselves 
                    with more kindness. They can become better leaders. They 
                    will start leading other people. So, firstly people are all 
                    right. And secondly they have a lot of resources so they 
                    deserve no blame. What the managers and leaders can do is to 
                    move to a blame-free type of management. We have to focus 
                    less on what is wrong and more on what we can learn from it 
                    and how to do it in a different and effective way in the 
                    future. That will become much more inspirational for the 
                    groups of people working together.<br>
                    <br>
                    <b>According to you what are the major leadership challenges 
                    of the 21st century?</b><br>
                    There are huge challenges we are facing today. There are 
                    stresses coming from everywhere, from our political system, 
                    family system, religious system, environmental system, 
                    educational system etc. People feel they are not allowed to 
                    explore creative solutions. But we need creative solutions. 
                    Coaching is an effective way to assist people to overcome 
                    these challenges and create a more productive working 
                    system.<br>
                    <br>
                    <b>
                    <img border="0" src="images/inter.jpg" align="right" hspace="12" vspace="6" width="250" height="70">Can you elaborate on the importance of Emotional 
                    Intelligence in coaching?</b><br>
                    Emotional Intelligence (EI) relates to flexibility, 
                    planning, communication and relationship skills that are 
                    necessary to influence and energise people. EI can be 
                    developed through the skillful redesign of internal dialogue 
                    habit. It works out when we can assist people to relax 
                    effectively and to communicate and visualise together. 
                    People will think effectively when they link their emotional 
                    awareness with the ability to act.<br>
