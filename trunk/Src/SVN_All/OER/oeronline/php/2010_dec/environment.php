				<font face="Verdana" size="1">
				<br>
                Fill Jute Bags…Not Plastic &amp; Go green’ is an eco awareness 
                programme initiated by Khimji’s Mart – the retail division of 
                Khimji Ramdas Group – to spread the concept of eco friendly bags 
                amongst Oman’s consumers. Aimed at educating the public about 
                the importance of being eco friendly and highlighting the 
                consequences of excessive consumption of plastic bags, this 
                eco-friendly programme began with the launch of Khimji’s own 
                signature shopping bag, made of jute natural fibre, in all its 
                supermarkets across the country. Shoppers at Khimji’s Mart were 
                encouraged to use these environment-friendly reusable bags as a 
                substitute to plastic bags.<br>
                <br>
                Taking its cue from the project initiated by the Environment 
                Society of Oman (ESO), Khimji’s Mart has set an example by being 
                the first supermarket in Oman to launch such a programme. The 
                initiative was launched at the Khimji’s Madinat Qaboos outlet 
                under the auspices of Her Highness Sayyida Tania bint Shabib Al 
                Said, chairperson of the Environment Society of Oman. In the 
                first phase of the campaign, Khimji’s Mart introduced 25,000 
                jute bags, which were placed at the entrance/cash tills of the 
                supermarkets. <br>
                <br>
                <b>A great success </b><br>
                The campaign has been a great success as it could attract a 
                large number of customers and it took only five months to clear 
                the total number of jute bags planned for a year. The initiative 
                could also reduce plastic bag consumption at Khimji stores by 10 
                per cent. Moreover, the campaign could highlight the necessity 
                of effective eco friendly initiative among other organisations 
                having similar interest such as Art of Living Muscat Chapter, 
                Haliburton etc. Oman Oil produced 10,000 Jute bags during the 
                Khareef festival in Salalah and Darsait School issued the text 
                books to the students in the jute bags supplied by Khimji’s Mart 
                at a very subsidised rate.<br>
                <br>
                <b>Innovative strategies <br>
                </b>The campaign was implemented in association with leading 
                business institutions across the country such as Oman Oil 
                Marketing Company, Oman LNG, Ali Abdul Karim Group, Al Khan 
                Foods etc. These partnerships have been greatly helpful in 
                distributing the jute bag at an affordable subsidißed rate 
                without putting extra liability on customers. Besides, the 
                message of the initiative was conveyed to the public through 
                continuous advertisements in different newspapers and magazines 
                and various in-store activities were organised in all Khimji’s 
                outlets across the country. With its strong presence in the 
                interiors of the country like Nizwa, Sur, Salalah, Sohar, Sumail 
                and many more areas, Khimji’s Mart’s initiative could spread the 
                concept of eco friendly bags amongst a large number of 
                consumers.
