                According to HE Ahmed bin Abdulnabi Macki, Minister of National 
                Economy and Deputy Chairman of the Financial Affairs and Energy 
                Resources Council, these reports indicate the stability and 
                strength of the Omani economy. As part of the diversification 
                strategy, the Al Duqm area is being developed as a modern 
                administrative, industrial, trade and tourist centre. The first 
                stage of the project includes a port, a dry dock, an airport, 
                power and water desalination plants, roads, tourism utilities 
                and infrastructure projects.<br>
                <br>
                Moreover, the government plans to build a railway line. Muscat 
                and Salalah airports are being upgraded, the construction of a 
                new domestic airport is underway and infrastructure projects are 
                being undertaken across the length and breadth of the country. 
                These include the development of information communication 
                technology (ICT) and tourism sectors. <br>
                <br>
