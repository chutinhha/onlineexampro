                <br>
                The Political Stability Index (2009), published by the Economic 
                Intelligence Unit, ranked the Sultanate No. 1 among the GCC and 
                Arab countries and 17 among 165 other countries. Similarly, the 
                Global Competitiveness Report 2008/2009 showed a remarkable 
                improvement in the country’s classification as its ranking 
                improved from 42nd to 38th among 134 countries.<br>
