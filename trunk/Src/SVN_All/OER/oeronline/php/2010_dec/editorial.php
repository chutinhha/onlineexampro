                investment. The government has amended investment-related laws 
                by allowing foreigners 100 per cent freehold rights and reducing 
                the income tax from 30 per cent to 12 per cent. The 8th 
                five-year development plan (2011-2015) focuses on accelerating 
                the relative contribution of the non-oil sectors to the GDP and 
                increasing skills and capabilities of Omanis. <br>
