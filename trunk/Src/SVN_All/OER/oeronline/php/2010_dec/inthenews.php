                    </font><b><font face="Verdana" size="4" color="#C00000">Beat Diabetes Walkathon evokes good response<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="2"><b>The Landmark Group’s initiative to raise awareness about 
                diabetes attracted an overwhelming participation of people from 
                all walks of life</b></font><b><font face="Verdana" size="4" color="#C00000"><br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px" dir="ltr">
				<img border="0" src="images/news.jpg" width="500" height="230"><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				<br>
                A strong crowd of 3,000 people assembled at Centrepoint Ruwi and 
                participated in 2.1-km ‘Beat Diabetes Walkathon’, which was 
                organised by the Landmark Group under the auspices of the 
                Ministry of Health on November 12, 2010. Dr Jawad bin Ahmed al 
                Lawati, Director, Prevention of Non-Communicable Diseases 
                Department at the Ministry of Health, kicked off the walkathon 
                by cutting the ribbon in the presence of Clive Freeman, 
                Territory Head of Landmark Group, Dr Sulaiman al Sherqi, Dr 
                Khulood Mufargi and a large number of participants.<br>
                <br>
                Commenting on the importance of the walkathon, Al Lawati says, 
                “I am impressed with the crowd. It shows that more and more 
                people are aware of the problem. The walkathon is successful in 
                sending the message that with some lifestyle changes the menace 
                of diabetes can be defeated. I thank the organisers for coming 
                up with a wonderful idea and developing awareness about the 
                disease.”<br>
                <br>
                “Our lifestyle gives very little scope for physical activities; 
                most of the people have cars and other modes of transport. There 
                is very little involvement of physical work even at the 
                workplace. Such events certainly play a role in developing 
                awareness about keeping fit,” he adds. Many participants 
                received free blood sugar and blood pressure tests at the venue. 
                To highlight the importance of fitness in fighting diabetes, a 
                fun filled fitness demonstration was conducted prior to the 
                walk. To add to the excitement drummers were stationed along the 
                route to cheer the walking crowd.<br>
                <br>
                As per Kims Oman Hospital, which had put up sugar check-up 
                stalls, about 2,500 people went through sugar and blood pressure 
                check-ups. The doctors found three per cent cases in the higher 
                range mainly among senior citizens and the 40 plus age group. 
                The participants started turning up from 7 am and in no time the 
                venue had a carnival look with people wearing blue T-shirt and 
                caps and kids holding balloons with their faces painted with 
                blue and white colour.<br>
                <br>
                Freeman of Landmark Group thanked the people of Oman for making 
                the event a grand success. “I am confident that this campaign 
                will grow much larger in the coming years as we plan to make 
                ‘Beat Diabetes Walkathon’ an annual event in Oman’s calendar”. 
                He also appreciated the guidance and support provided by the 
                Ministry of Health, Royal Oman Police, Muscat Municipality and 
                the sponsors. <br>
                <br>
                <b>Celebrity show </b> <br>
                The walk saw celebrity Ekta Chowdhry, winner of Femina Miss 
                India Universe 2009, taking to the streets along with achievers 
                from Oman like Saleh al Jabri, captain of Jewel of Muscat, Hiba 
                al Rahbi from Royal Cavalry, Nizar al Shanfari, rally driver and 
                Sara al Balushi, rising tennis star of Oman, pledging their 
                support for spreading awareness about diabetes.<br>
                <br>
                Says Chowdhry, “My mother suffers from diabetes and I know as to 
                how deadly a disease like this can be. It is important to spread 
                awareness about Diabetes and if someone can make a video of 
                today’s event and put it on YouTube, it would really help.”<br>
