                    <br>
                    </font><font face="Verdana" size="2"><b>Since Omani economy 
                    has grown steadily even after the financial crisis, there 
                    has been no “crash” in the real estate sector in the country<br>
                    <br>
                    By Sudhakar Reddy</b></font><font face="Verdana" size="1"><br>
                    <br>
                    Real estate prices depend on a number of factors such as 
                    incomes, interest rates, zoning regulations, restricting 
                    supply etc. Therefore, one wonders why, at times, real 
                    estate prices move away from fundamentals. There are several 
                    reasons.<br>
                    <br>
                    The first is that the relatively small part of the total 
                    stock is actually bought and sold in a given period of time 
                    and the prices at which this small quantity changes hands 
                    determine prices of the  whole stock.<br>
                    <br>
                    A second reason is that, unlike equities, real estate cannot 
                    be “shorted”. Therefore, a market participant cannot take 
                    advantage of prices rising beyond what the fundamentals 
                    dictate and short the market.<br>
                    <br>
                    A third reason is that the owners see their equity in their 
                    homes rise and use it to move into even better houses, 
                    thereby pushing up the prices for these too. A fourth reason 
                    is that rise in housing prices can be self reinforcing as 
                    buyers believe that they will rise further.<br>
                    <br>
                    Yet another reason is that central banks keep interest rates 
                    at ultra low levels to spur growth and employment and this 
                    actually leads a lot of money going into assets and 
                    resulting in asset price bubbles.<br>
                    <br>
                    Each asset price bubble follows rapid credit growth. The 
                    great depression was preceded by enormous growth of credit 
                    to margin money lending against stocks. Similarly the “great 
                    recession” of the present day, was preceded by rapid growth 
                    of credit especially to the housing sector in the developed 
                    economies.<br>
                    <br>
                    <b>Prudent policy</b><br>
                    In the GCC countries and especially in Oman, government 
                    spending is the main driver of the economy and this depends 
                    to a large extent on oil prices. Thus even after the asset 
                    bubble burst, the Omani economy has not been affected since 
                    oil prices are still high by historical standards and the 
                    government is able to finance its spending. Of course, the 
                    government has been very wise in saving when oil prices were 
                    high. Thus government spending has not followed the wild 
                    fluctuation in oil prices and has been growing steadily. The 
                    Oman economy has therefore been growing steadily even post 
                    the financial crisis. Therefore, although real estate in 
                    Oman have corrected, there has been no “crash” as in some of 
                    Oman’s neighbours.<br>
                    <br>
                    <b>Stability</b><br>
                    The big question is whether the present real estate prices 
                    in Oman reflect fundamentals of incomes and interest rates 
                    hence demand and supply. We think they largely do. <br>
                    <br>
                    The per capita nominal GDP has almost tripled compared to 
                    what it was some years back and we therefore can no longer 
                    expect prices to go back to what they were before the boom. 
                    In addition, inflation in cost of construction material has 
                    pushed up cost of construction costs by about 50 to 60 per 
                    cent compared to the pre boom period. Thus there is cost 
                    push inflation as well as demand pull inflation in real 
                    estate prices.</font></p>
