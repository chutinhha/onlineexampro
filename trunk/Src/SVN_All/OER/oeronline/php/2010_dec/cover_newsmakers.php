				<font face="Verdana" size="1"><br>
                Freezone Sohar (FzS), an ambitious free zone venture taking 
                shape alongside the Port of Sohar, has been attracting the eyes 
                of international investors. Authorities have drawn up 
                investor-friendly regulatory policies and a range of fiscal 
                incentives under a freezone law to be promulgated soon. Freezone 
                Sohar is being developed by Sohar International Development 
                Company (SIDC), a 50:50 joint venture between the Government of 
                Oman and Port of Rotterdam, and SKIL Infrastructure Limited of 
                India.<br>
                <br>
                Freezone Sohar, says Aziz, has an enormous investment appeal 
                given its proximity to a world-class port; the planned 
                establishment of an international airport and rail network, and 
                other transport infrastructure in the Batinah region. The 
                freezone aims to attract downstream investments that add value 
                to base industries, chiefly petrochemicals and metals, currently 
                in operation at the Sohar Port.</font><p align="justify" style="margin: 10px; ">
