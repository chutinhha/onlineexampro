				<font face="Verdana" size="1"><br>
                John Malcolm, former managing director of Petroleum Development 
                Oman (PDO), in September, was conferred “Oman Civil Order” Third 
                Class by His Majesty Sultan Qaboos bin Said in appreciation of 
                his good efforts in promoting the Omani oil sector. Malcolm had 
                spearheaded the 60 per cent government owned PDO since November 
                2002 with his single point agenda being to enhance the oil 
                production of the company. For a country which relies heavily on 
                oil production to generate its national income, Malcolm’s role 
                was to find ways and means to reverse the falling oil production 
                of the company. He led a three-pronged EOR effort using thermal, 
                chemical (mainly polymer injection) and miscible gas. Malcolm 
                said the share of PDO’s liquids produced by EOR will rise to 36 
                per cent in 2018 from 3 per cent in 2008.</font><p align="justify" style="margin: 10px; ">
