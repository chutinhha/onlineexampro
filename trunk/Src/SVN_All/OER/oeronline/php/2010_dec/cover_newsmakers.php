				<font face="Verdana" size="1">Omantel and Oman Mobile, which operated as two separate entities 
                for fixed and mobile services respectively were merged to 
                operate as a unified structure. The new operating model brought 
                both company brands under a single umbrella without losing the 
                brand equity of either Omantel &#8232;or Oman Mobile. <br>
                <br>
                Omantel CEO Dr Amer Al Rawas, said the new tagline “Together”, 
                is not just a visual symbol, it illustrates the wider change 
                within the company. This signals our entry into a new era. The 
                new stage will focus on improving customer experience.<br>
                <br>
                Omantel also introduced a slew of packages to Mada post-paid 
                service as well its Hayyak pre-paid service. The Hayyak off-peak 
                rates have been lowered from 45 to 39 bzs a minute and it 
                announced the introduction of a new daily off-peak time between 
                8 pm and 6 am. <br>
                <br>
