                and Publishing House, embarked upon changing the face of the two 
                dailies published by the company – Times of Oman and Al Shabiba.<br>
                <br>
                Today, both the newspapers have a stunning new look with a 
                complete facelift both in terms of design and editorial; size of 
                the papers; as well as contemporary masthead logos. The new size 
                followed by Times of Oman and Al Shabiba is used by most 
                well-known dailies like USA Today, Wall Street Journal, Times of 
                India etc. Ahmed has also been responsible for technological 
                innovations like introducing WAP newspaper as well iPad version 
