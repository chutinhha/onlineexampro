				<font face="Verdana" size="1"><br>
                Activities are underway for celebrating Oman LNG’s 10 years of 
                operations in the Sultanate and partnership with the government 
                to achieve His Majesty’s 2020 goals of sustainable economic 
                growth and diversification. Dr Brian Buckley, GM &amp; CEO believes 
                that gas has been the game-changer of the last decade for Oman 
                as gas has become the largest single revenue generator for Oman 
                after oil and the greatest catalyst for the diversification of 
                Oman’s economic base.<br>
                <br>
                Oman LNG stands as a major contributor to the Sultanate’s 
                revenue stream and has attracted many laurels within only a 
                decade as an active player in the global LNG industry. In 2002, 
                only two years into operations, the company achieved its first 
                nine million man-hours without Lost Time Injury (LTI). At 
                present the company contributes around 10 per cent to Oman’s 
                GDP.</font><p align="justify" style="margin: 10px; ">
