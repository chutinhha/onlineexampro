            </font><font face="Verdana" size="1"><b>Soul of a sports car in the 
            body of sedan is how Mazda would describe its 2.3-litre Mazda6 MZR. 
            Vicky Kapur finds out the truth behind that statement</b><br>
            <br>
            <img border="0" src="Car-review-Mazda6-copy.jpg" align="left" hspace="5" vspace="5" width="300" height="146">Allow 
            me to be forthright - this is my first car review for OER. Having 
            acquired my Oman driving licence in late September, when approached 
            by Towell Auto Centre last month for test driving the Mazda6 MZR 
            2.3, I was a bit hesitant. The tentativeness could be attributed to 
            the fact that I wasn't sure if it would be me test driving the car 
            or the other way round. <br>
            Once behind the wheels of the sport sedan, though, I was glad I took 
            up the challenge. Underneath those sleek, sexy curves of the 
            mid-sized is an engine whose performance matches that of a sports 
            car. Unlike the run-of-the-mill, 4-door family haulers that clutter 
            the Omani roads, the MZR delivers the goods. The 166-hp, 2.3-litre 
            in-line four, with its counter-rotating balance shafts, gives you 
            that great 'in-control' feeling: smooth when cruising; crisp when 
            not. <br>
            On the road, I couldn't help but realise the truth in a statement 
            that my driving instructor used to make: a better driving experience 
            often requires a better car. Like a car with a sport-tuned 
            suspension system that helps you attack corners more firmly and 
            precisely. Like a car with friction-optimised rack-and-pinion 
            steering that makes manoeuvring serpentine bends on the road a 
            one-finger affair (don't try this at home). Like a car with 4-wheel 
            disc brakes that give you confidence that every press of the pedal 
            yields inspiring stopping power. Like a car power-packed to deliver 
            166 hp @ 6500 rpm with manual-shift mode that allows you to find 
            just the right gear for that extra burst of power on a steep incline 
            or at a particularly busy roundabout. <br>
            With all that plus a remarkably versatile interior, the MZR has 
            'driving pleasure' written all over it. On the road, the car 
            practically sings with liveliness. It turns briskly into corners 
            with a kind of athletic purpose, evidence of the development put 
            into the control-arm front suspension and uniquely packaged 
            multi-link rear setup. You'll be excused for not being terribly 
            scared of throwing the sporty sedan into corners like a rally car 
            because it doesn't lose its poise when the tyres are working hard.
            <br>
            The Mazda6 MZR is, indeed, a sport sedan, and its high content of 
            engineering and luxury features shows you that it takes this mission 
            seriously. At the same time, it has a simplicity that makes it 
            affordable. With a price-tag that starts at RO7,000 and goes up to 
            RO8,000, this powerhouse of a car sure gives you economical 
            efficiency. <br>
            <br>
            <b>Exteriors </b><br>
            The MZR combines bold styling, dynamic performance and outstanding 
            handling with ample room and comfort. The new front-end is a work of 
            art. If you look closely, you're sure to count no less than four 
            xenon globes behind the smoked lenses of the new-look headlight 
            cluster and grille work. Obviously, this not only looks nice, but 
            also delivers the goodies when the sun goes down. <br>
            The tail end, in contrast, may be too much for some, with its glitzy 
            sliver-backed clear-lens brake light cluster. My personal opinion, 
            though: I don't mind. Other styling aspects, such as the flared 
            wheel arches and front apron give the car a more purposeful look 
            than those of its predecessors. Its body architecture is slightly 
            wider than the previous generation of mid-sized vehicles while that 
            extra width contributes significantly to its solidity and stability.
            <br>
            Its broad stance also reflects a widening of the wheel track by 50 
            mm. The extra width effectively drops the MZR's centre of gravity by 
            20 mm, a significant measure in shaping the stability of the 
            vehicle's handling characteristics. At the front, the MZR features 
            high mount double wishbone suspension and a friction-optimised 
            rack-and-pinion steering system mounted on a rigid perimeter frame.
            <br>
            At the rear sits E-type multi-link suspension, which is expected to 
            deliver improved ride and handling. The car is available in 10 
            colours: snowflake pearl white, pacific blue, strato blue, canary 
            yellow, classic red, sparkling silver, silver contrail, titanium 
            grey, garnet red, and black.<br>
            <br>
            <b>Interiors</b><br>
            The MZR features a wide range of creature comforts like power 
            windows and locks, remote keyless entry and 17-inch alloy wheels 
            besides the option of an in-dash single- or 6-disc CD changer. 
            Makers of the MZR followed an insightful approach to interior 
            design: maximum room for shoulders and legs (not just in the front, 
            but rear as well). <br>
            Generous storage space is offered by the glove compartment's nine-litre 
            capacity, and there are a number of cleverly designed storage areas. 
            Ergonomic conditions are optimised for the driver, with a flexible 
            driving seat and steering wheel. This is combined with a stylish 
            dash area with titanium-look interior finish and, of course, 
            luxurious upholstery. Further innovation featured in the MZR is the 
            'Karakuri' rear seats, which fully flatten with just the pull of a 
            handle. <br>
            The multifunctional steering wheel allows you operate cruise control 
            as well as the various functions of the modular audio system with CD 
            player. The front and rear centre arm rests, leather gear-knob and 
            leather handbrake lever add a touch of class too. The MZR also 
            features an electric tilt-and-slide sunroof. <br>
            <br>
            <b>Performance </b><br>
            There's a lot more to performance than just going fast. Performance 
            is having a perfect balance between speed and stability, being able 
            to zip through an S-curve with sure-footed handling and precise 
            steering response. It's a lesson that engineers behind the MZR have 
            themselves been teaching for years with sports cars like the MX-5 
            Miata and RX-7. <br>
            The Mazda6 gets things rolling with its new 2.3-litre MZR engine 
            available with a four-speed automatic transmission with an 
            Activematic Transmission Control System to provide manual (tiptronic) 
            gear selection. Mazda's G5M five-speed manual gearbox, with gear 
            ratios matched to the engine's torque curve, helps deliver improved 
            acceleration for a 0-100 km/h sprint of 8.9 seconds. <br>
            <br>
            <b>Safety </b><br>
            Vehicle safety is something a driver should be aware of but not have 
            to worry about. With the MZR, you can concentrate on the driving, 
            knowing that it is built with some of the most advanced safety 
            features available today. Like advanced dual-stage front air bags 
            with crash zone and driver's seat position sensors, an available 
            Anti-lock Brake System with Electronic Brakeforce Distribution, plus 
            a sophisticated body structure and numerous structural components 
            that help contribute to a NHTSA 5-star safety rating in front 
            impacts. <br>
            Safety in the MZR begins with the structure of the chassis, with a 
            &quot;Triple-H&quot; body structure designed to crumple and help absorb energy 
            during a collision before it reaches the passenger compartment. 
            Advanced front-impact air bags are standard front. Side-impact air 
            bags and side air curtains are available with certain option 
            packages. All five seating positions are equipped with 3-point 
            safety belts. Side-impact door beams help provide additional 
            protection in side collisions <br>
            <br>
            <b>The finishing line </b><br>
            It's your true, authentic, bona fide sports sedan that does its 
            sports-car DNA proud. Like siblings MX-5 Miata and MazdaSpeed 
            Protégé, the MZR is a driver's car - a fact made readily apparent by 
            its spirited response and superb handling. Visual impact is often 
            the deciding factor when buyers are looking for a mid-sized vehicle, 
            as is engine performance. On these two aspects alone, one would have 
            to assume that the MZR is in a win-win - or should that be zoom-zoom 
            - situation. <br>
