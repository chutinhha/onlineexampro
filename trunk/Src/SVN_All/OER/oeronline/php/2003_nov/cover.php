            customer than it does to get repeat business from an existing 
            customer. Marketing pros will even have us believe that although the 
            value of a satisfied customer can't be measured tangibly, it goes a 
            long way in ensuring the business success of any organisation in any 
            industry.<br>
            Electronic and print ads bombard us with promises to make us happy 
            as buyers. Experts vouch that the customer's satisfaction is of 
            utmost importance to any company's success. Management tomes keep 
            emphasising the need to treat customers both 'royally' and honestly, 
            creating a dialogue, making the effort to determine their 
            requirements - i.e., offering them a great service. In a nutshell, 
            all the clichés that first year marketing students are fed in their 
            theory lectures. <br>
            Unfortunately, more often than not, it stays just that - theory. In 
            practice, one could safely wager a bet on the fact that roughly half 
            the people reading this article will have horror tales of their own 
            to tell of the abominable &quot;customer service&quot; they may have 
            encountered in the last six months. It's an absolute shame to see 
            too many businesses - some small and a lot large - forget the 
            strategy of profit maximisation through repeat sales. <br>
            You won't have to look too hard around you to find a business 
            without proper customer service attitude arguing over a RO5 mistake 
            with a customer who generates RO500 per year in sales. Ridiculous, 
            isn't it? Even though everyone in their right marketing mind will 
            answer that question in the affirmative, how many of us really 
            follow the principle of 'customer is king' in its right spirit? <br>
            Customer satisfaction holds the key to ensuring repeat business from 
            existing customers, which has become so necessary in these times of 
            a global slowdown and increased competition. Customer service is no 
            longer an optional part of any organisation's marketing plan. <br>
            <br>
            <b>Service begets satisfaction </b><br>
            With increasing commoditisation of products in our market, the 
            customer is seeing service excellence as the most important 
            differentiator between two businesses or brands with perceptibly 
            similar products. <br>
            The worth of satisfied customers to building a successful, growing 
            business cannot be overstated. While many companies work hard to 
            increase sales, they overlook the importance of doing the little 
            things that keep customers happy and buying more. Worldwide, 
            customer retention has become the Holy Grail in industries from 
            airlines to air-time. <br>
            As experts will affirm, even if you absolutely know that the 
            customer is manipulating you, make every attempt to satisfy their 
            complaint, because if you don't, they can eliminate a dozen 
            prospects by bad-mouthing you. You can always choose not to do 
            business again with chronically-complaining customers, once you do 
            your best. The following facts were revealed in a US 
            government-funded study: <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> </font><font face="Verdana" size="1">
            The average business never hears from 96 per cent of its unhappy 
            customers.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> </font><font face="Verdana" size="1">
            Of customers who register a complaint, between 54 and 70 per cent 
            will do business again with the company if their complaint is 
            resolved. <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> </font><font face="Verdana" size="1">
            The average customer with a complaint tells 9 or 10 people about it.
            <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> </font><font face="Verdana" size="1">
            Customers whose complaints are well taken care of will tell five or 
            more people about the treatment they received. <br>
            <br>
            Many organisations do not realise that even relatively minor 
            improvements to customer service can significantly improve their 
            bottom line and deliver better value. Consider the following: <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            A Daily Telegraph-British Telecom survey of 600 senior UK managers 
            claims that the average rate of return of investment into improving 
            customer service is about 10 per cent.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            In 73 per cent of cases where organisations made no attempt to 
            persuade dissatisfied customers to stay, more than half of those 
            customers said that a simple apology would have precluded them from 
            moving to the competition. <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Reducing customer defections can boost profits by anything from 25 
            per cent to a staggering 85 per cent.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            A report by the US Institute of Customer Service estimated that 
            American companies spend a total of $84 billion (RO32.31 billion) 
            every year compensating customers for bad service. <br>
            These statistics are positive proof that paying particular attention 
            to customer service is a financially rewarding investment. <br>
            <br>
            <b>Service by design </b><br>
            An effective customer service strategy, well executed, provides the 
            most treasured asset for a business - repeat customers. The goal of 
            effective customer service is simple, really. It is making your 
            customers as excited about your product or service as you are. Good 
            customer service starts with you. Ask yourself - 'Even when I am 
            under time pressure, do I take the time to really listen to the 
            customer's need or complaint? Am I prepared to offer a satisfactory 
            response? And do I follow up quickly?' If you aren't confident that 
            you can provide the level of service consistent with this type of 
            response, you need to rethink your customer service strategy. <br>
            &quot;Do as I say, not as I do&quot; may work with some young children, but it 
            rarely works with adults, particularly employees. You have to 
            personally be true to your customer service plan every day and make 
            it obvious to those who work with you that they must satisfy your 
            customers. This includes employees, suppliers, even your delivery 
            company. <br>
            It is relatively easy to be accommodating when your business is 
            small or you do not have many customers. As your business grows, 
            though, so do the demands on your time and patience. And therein 
            lies the real test of customer service. However, you would be amazed 
            at how little it takes sometimes to make someone feel really well 
            taken care of. Most people are reasonable in their demands - they 
            want you to show you really care in exchange for their hard-earned 
            money. <br>
            A mere 'thank-you' produces a lot of smiles. Develop your own 
            thank-you system. Some use attractive thank-you notes. Others call 
            every new customer after their first order to check their 
            satisfaction and to tell them they appreciate their business. Smart 
            marketers send customers surveys out from time to time and promise a 
            small gift for returning them. <br>
            Many successful small business marketers provide information and 
            education on how to better use their products and services. Some 
            companies sponsor contests among their customers - to pick a product 
            name, guess a name or number or come up with the most unusual use 
            for their product. The winners are prominently communicated by the 
            company to all of their other customers. <br>
            <br>
            <b>Steps to service </b><br>
            Customer service is one area where even a small business can 
            effectively compete against larger conglomerates. Providing 
            comprehensive satisfaction, though, stays paramount. To create a 
            successful customer service strategy, you should think carefully 
            about the answers to the following questions: <br>
            1. How easy is it for your customers to reach you personally? <br>
            1 a. How can you make it easier?<br>
            2. Do your customers have to talk to more than one person to receive 
            service? <br>
            2 a. How can you cut the red tape? <br>
            3. Are your pricing policies clearly explained? <br>
            4. Are your credit, return and special order policies designed to 
            benefit your customers? Or your accounting system? <br>
            5.Can you honestly say that your product or service delivers the 
            benefits your advertising claims? <br>
            6. Do you make it easy for your customers to order? To pay? <br>
            7. Do you give your customers an easy way to make complaints? <br>
            8. Have you trained everyone who will deal with customers in the 
            basic feature-benefit story of all of your products or services? <br>
            9. Is your business listed in the local pages of the telephone 
            directory? The Yellow Pages? <br>
            10. Have you examined a toll-free phone number for placing orders?
            <br>
            11. Does your company name, address and phone number appear 
            prominently on all of your sales literature? <br>
            12. If in retail, do you provide directions on how to reach you? <br>
            13. For retail, is there ample parking near your store? <br>
            14. Do you inform customers of delays in delivery?<br>
            14 a. If no, how can you correct this? <br>
            15. Is your employees' salary linked to sales? <br>
            15 a. Is it really working for you? </font></p>
            <p><font face="Verdana" size="1">Often, successful customer service 
            consists of using the right words at the right time. This is 
            particularly important when the customer is upset or disappointed. 
            Some very effective phrases include: <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            I'll take care of that for you. <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            I take full responsibility.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            We want your business. <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Thank you for thinking of us. <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Consider it done. </font></p>
            <p><font face="Verdana" size="1">Good customer service derives from 
            treating others the way you would like to be treated. No more, no 
            less. Says S. Kasturirengan, general manager, Towell Auto Centre 
            LLC: &quot;While standard policies are there as guidelines, the most 
            important element that all our staff are instilled with is that&quot; 
            treat the customer as you want to be treated&quot;. We have a centralised 
            customer relationships department which contacts customers on every 
            visit to the Mazda showroom or Mazda service reception.&quot; <br>
            <br>
            <b>Service @ work</b><br>
            The ability to accurately identify and satisfy your primary 
            customers is the key ingredient of your success in the world of 
            work. Successful leaders, either in small business or larger 
            organisations, are continually thinking about their customers and 
            how they can please them better than their competitors. As a result, 
            they are constantly finding creative ways to win and keep customers 
            more effectively than others. <b>Money is important, but only just:</b> 
            Revenue will come to you once you manage your business well and 
            satisfy your customers. Create a strategy of serving your customers 
            better than your competitors - then watch the profits roll in. <b>
            Stay with your customers:</b> And they'll stay with you. If you want 
            repeat business, you need to understand that your first sale was 
            just the beginning of a good sales strategy. The second will depend 
            upon how good your products or services were and what value you 
            added. At all times, provide your customers added value, anticipate 
            their needs and follow up. <br>
            <b>Get your customers involved:</b> Include your customers' opinions 
            in your strategic decisions. Ask them how they like your services; 
            ask them what else needs to be added to your existing line of 
            services. You'll not only get fresh ideas, but also gain your 
            customer's loyalty. <br>
            <b>Perception is reality:</b> Improve your customer's perception. 
            The way your customers feel about you is their reality. Make sure 
            you illustrate an image that you want them to have about you. <br>
            <b>Communicate is critical:</b> Your customers need to see your name 
            even after the sale is made. Publish a newsletter, share your 
            successes with them, announce your products and services. All these 
            are simple but essential tactics in maintaining a satisfied 
            customer. Let your customers know what you do. <br>
            <b>The product-plus paradigm:</b> Give more to your customers than 
            what they actually paid you for. You don't have to go to the point 
            of harming your cash flow, but extras do count for maintaining a 
            healthy relationship. Find out what your customer values - go that 
            extra mile. <br>
            <b>Accountability matters:</b> In case of complaints, you or the 
            employee that caused the dissatisfaction should find the solution to 
            your customer's problem. Make your employees or yourself accountable 
            to correcting difficulties. <br>
            <b>Be flexible:</b> Don't carve policies in stone. There are so many 
            situations that occur during the course of a business day. Keep in 
            mind that special circumstances can, and do, arise frequently. When 
            the situation warrants, be flexible with your company's policies.
            <br>
            <b>He who comes back, comes for good(s):</b> Your best customers are 
            the repeat ones. These are also the ones most likely to bring you 
            referral business. As you look at your customer service strategy, be 
            sure to include flexibility, creativity and the constant adding of 
            valuable services. Remember, your customers' success is your own.
            <br>
            <br>
            <b>Complaint compliance </b><br>
            Complaints can cause you to lose future sales from customers and 
            from everybody else who hears about their bad experience. Or you can 
            turn customer complaints into more sales from these same customers 
            and the people they influence. How you handle your customers' 
            complaints determines which of these two results you get. Here are 
            some simple steps you can take to turn customer complaints into more 
            sales. <br>
            <b>Plan for the inevitable:</b> You can never eliminate every 
            customer complaint. Handle complaints with a positive attitude. 
            Strive to preserve your relationship with the complainant instead of 
            your immediate profit from them. Make your customer happy now and 
            they will reward you later with more sales. <br>
            <b>Move fast:</b> Surprise your customer with a quick response to 
            their complaint. If you cannot solve the problem immediately, let 
            them know you consider it a priority. Then do whatever you can to 
            resolve the problem fast. The longer a customer has to worry about 
            getting their problem solved, the less likely they will accept a 
            satisfactory solution. <br>
            <b>Act professionally:</b> Even when a complaining customer does 
            not. Customers sometimes act hostile because they expect you to 
            resist solving their problem. You can calm their hostility by 
            letting them know you genuinely want to help them. Assure them you 
            will do everything possible to solve their problem. <br>
            <b>Sorry, my mistake: </b>Take responsibility for resolving your 
            customer's complaint even if the problem was not your fault. 
            Apologise for the inconvenience. Briefly explain the probable cause 
            of the problem. Then tell your customer exactly what you will do to 
            correct it. Don't focus on blaming someone else for the problem. It 
            sounds like an excuse. <br>
            <b>Offset the oversight: </b>Complaining customers hope they can get 
            a satisfactory solution. But they often expect to get something 
            less. Surprise them by solving their problem and giving them 
            something extra to compensate them for their inconvenience. This 
            will help them forget about their problem and, instead, remember the 
            special attention you give them. <br>
            <b>Confirm satisfaction:</b> After solving your customer's problem, 
            follow up to confirm their satisfaction with the outcome. This 
            reinforces your relationship with the customer. <br>
            <b>Stem the rot: </b>After resolving a customer complaint, try to 
            identify exactly what caused it. A complaint often reveals some 
            weaknesses in your business procedure. Many times, this weakness is 
            minor and you can easily correct it to avoid similar complaints in 
            the future.<br>
            Probably the most sincere form of caring about your customers is to 
            keep your promises. It is very easy in business to over-promise. You 
            desperately wish to please everyone. You may take on too many jobs 
            at one time. Or you may accept work that requires skills you do not 
            currently possess. It is better for your ongoing relationship with 
            the customer to ask if the job schedule can be extended or to 
            honestly tell them that the skill required is not a strong point for 
            you. <br>
            Remember, your customers are an excellent source of new product and 
            service suggestions. Their needs change over time and if they feel 
            you care, they will tell you about their new wants. At the end of 
            the day, what most people want is good value for their money and 
            some sense that what they purchase is appreciated. Are you providing 
            that?</font></p>

