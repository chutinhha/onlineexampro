                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Twilight Zone
                    </font><font face="Verdana" size="4" color="#FF0000">&nbsp;</font><p><font face="Verdana" size="1"><b>While some automobile dealers are betting big on pre-owned cars business, a few others are scaling down their operations, Sahad P.V reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><img border="0" src="../../images/2002_july/used-cars-2.jpg" align="left" hspace="5" vspace="5" width="225" height="174"><font face="Verdana" size="2">What does a young Omani aspire for soon after the completion of his training and education? A job at first, and next a car, his basic need for travel! The very fact that public transport system in Oman has predominantly remained underdeveloped calls for the possession of a personal car for a young Omani intending to join the workforce sooner or later. Naturally, he first looks at the options available within the market, and he either buys a new car offered under attractive instalment package or, if he has a bit of cash, most probably, picks up a used car. Envisioning the potential growth for the used car market, automobile majors like Bahwan Automotive Centre (BAC), have recently launched Best Cars, the largest showroom for used vehicles in the country.<br>
                    "Oman is a very price sensitive market and there are a large number of customers looking for good quality, pre-owned vehicles. Indeed, there is a need for outlets where they can buy cars with confidence and trust," explains a BAC spokesperson.<br>
                    Statistics suggest that more than 40 per cent of the 2.5 million Omani population belongs to the 15-18 age group, and a huge part of this group is expected to join the working sector within a year or two. "In such a situation, there will be a good demand for entry-level used cars from such a segment of population," says a senior manager of a leading auto company in Muscat.<br>
                    Also, what makes the Sultanate an attractive market for used cars is its low per capita income at US$7,700 (RO3080) - the lowest in GCC as compared to Saudi Arabia - US$9,600, and UAE -US$22,800. This is the reason why small cars are most popular in Oman, while consumers in Saudi Arabia and UAE prefer big cars – mainly American models from Ford and General Motors.</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_july/used-car-1.jpg" align="right" hspace="5" vspace="5" width="225" height="173">Thus, the potential for used cars in Oman is great. That is why, from a flea market type of operation until recently, the used car business in Oman is fast assuming an organised industry status. Some of the established authorised car dealers like Towell Auto and Moosa Abdul Rahman already have their used car business divisions in operation for the past two years in addition to small operators like Gulf Auto, a company that has been exporting used cars to various parts of the world.&nbsp;<br>
                    While Towell Auto Centre’s Bait Al Sayarat and General Motors’ Advantage have been giving some semblance of an order to the used car business, BAC’s latest entry is likely to bring the much-needed sales push to this developing sector.<br>
                    BAC’s showroom in Wattayah has a stock of about 200 vehicles at a time and has nearly 200-300 vehicles in its seven other branches countrywide. "Best Cars is Oman’s largest pre-owned automobile hypermarket, where customers can shop around for vehicles at prices ranging from RO300 to RO30, 000," says the BAC spokesperson.&nbsp;<br>
                    <br>
                    <b>The challenges</b><br>
                    Will auto companies’ gamble with huge showrooms, separate sales and service teams for used car business work? In fact, used car business, as a separate profit centre has not always paid off in Oman. For instance, Towell Auto Centre began Bait al Sayarat as an individual profit centre and later discovered that the huge capital investment required cannot justify the returns. Later, this division ceased to be a separate entity and was merged with its main car business.&nbsp;<br>
                    "We were one of the first companies to get into the used car business on a big scale. But afterwards we discovered that used car prices fluctuate and new models come in every three months driving down the prices," says Praveen Thampi, National Manager, Towell Auto Centre.&nbsp;<br>
                    The key to success in used car business is the fast movement of cars. Sources indicate that if the stock stays for more than 30 days, it will burn the dealer’s fingers. "Every six months there is a major turnaround. Especially in July and August, a lot of new cars come into the market. It’s not a healthy situation for the used car business. The best thing is to have a normal operation (along with new cars)," says Thampi. A separate entity means investment in space, service centre, people, back-end operations, and of course, marketing and branding costs, he adds.<br>
                    Besides, the currency fluctuations could also affect the prices of the used cars. For instance, if Japanese currency yen depreciates against dollar, then the customers immediately start asking for a reduced price for the Japanese car models.<br>
                    M.M. Thomas, General Manager - Automotive Sales Division, Mohsin Haider Darwish, the dealer for high-end cars like Land Rover, Defender, Discovery and Jaguar, too thinks a separate showroom for its used car business will not be a good idea. "We are not in the volume business. A separate showroom and a sales and service team would be expensive."<br>
                    However, there is no dispute on the potential of the used car business. "The numbers are increasing. There is a huge potential. But I will do the used car business through the new car operations," says Thampi. Also another reason is that the trade-in ratio between new vehicles and used vehicles is very high - almost touching 1:1 - which makes buying used cars directly from the market difficult.<br>
                    However, BAC spokesperson defends such a massive scale of operation. "Best Cars has been well accepted by the consumers and our volumes have gone up substantially since the launch of our operations. We see good potential for growth in the future."<br>
                    The idea behind the concept of a separate showroom for used cars is to give the customer a buying experience which he otherwise misses out on in an unorganised atmosphere. For most of the used car buyers, it could be the first car. So the dealers are trying to sell a concept by setting up swanky showrooms.<br>
                    Says S Hari, manager in charge of used car business of Al Hashar, "We may also think of starting a showroom for used cars once the business picks up." Currently, Al Hashar has a stock of about 400 used vehicles in various yards across the country.&nbsp;<br>
                    <br>
                    <b>The number game&nbsp;</b><br>
                    As for the used car sales, the real numbers are not forthcoming. BAC, however, could emerge as the leader in the used car sales too as it leads in the new cars sales with more than 50 per cent market share. As for Towell, sources say it sells about 30-35 used cars a month, while Al Hashar, the dealer for Nissan cars, sells about 60 cars.&nbsp;<br>
                    So what kind of volume does used car business generate? According to industry estimates, used car sales – including the unorganised sector – would be about a quarter of total car sales. According to independent estimates, the total number of new cars being sold in Oman ranges from 21,000-22,000. The exact numbers are hard to come by, as Royal Oman Police (ROP) does not publish the car registration data.&nbsp;<br>
                    So what is in store for the used car business? In countries like the US, Europe and Japan, the used car sales are very large and comparable to new vehicle sales. In UAE, too, the used car sales have, in fact, driven the new vehicle sales, whereas in Oman the used car market is expected to grow as car dealers experiment with more value-added products to attract customers.
