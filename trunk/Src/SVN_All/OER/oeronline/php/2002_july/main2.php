                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The retail rush</font><p><font face="Verdana" size="1"><b>RGO-owned Al Burj is Oman's answer to a host of multinational shopping complexes in the country, but consumers are a confused lot with the proliferation of shopping malls, reports Janaki Raghavan</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_july/AL-BURJ-2.jpg" align="left" hspace="5" vspace="5" width="225" height="195">
                    Standing solitarily on an off road in the Seeb township is an innocuous looking building seemingly devoid of any activity. But only until you drive towards it, you find the car park jam-packed, and the place bustling with busy shoppers. You’ve reached Al Burj, the most-recent hypermarket in Oman that aims to tap the retail industry in a significant way.<br>
                    At a time when there seems to be less room for one more hypermarket, Al Burj is being considered as the country’s answer to a host of multinational supermarkets and shopping complexes that have already captured the local market. And the huge crowd thronging at Al Burj seems to indicate the same. Current promotional hype has paid off and the hypermarket appears to be a serious contender for claiming its share of the market. Though there are quite a few well-established supermarkets in Oman, incidentally there are just two hypermarkets - Carrefour in City Centre and the RGO Welfare Shop in Al Burj.&nbsp;<br>
                    “The complex is the latest venture of Al Burj Trading and Consumers LLC and is a 100 per cent Omani company, owned and managed by the Royal Guard of Oman (RGO) Pension Fund,” says Tameem Saif Majid Al Mahrooqi, Director of Al Burj. The City Centre in Oman is the only other shopping complex anchored by a hypermarket, and is a Dubai-based venture, he adds.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_july/mahrooqi.jpg" align="left" width="150" height="174">
 Besides, Al Burj has the distinction of housing Oman’s first electronic superstore - Souq Technia. “Our complex has a tenant mix that comprises retailing, speciality shops, essential service outlets, banking facilities, hypermarket, food court, household furniture and electronics and home appliances,” says Mahrooqi.&nbsp;<br>
                    <br>
                    “Al Burj Complex in its new convenient family-oriented format is steadily becoming an attractive shopping destination for the discerning customer,” observes Swaminathan, Senior General Manager - Business Development, Oman Trading Establishment (OTE) group which in collaboration with Al Burj Trading has set up Souq Technia.<br>
                    <br>
                    <b>
                    Great potential</b><br>
                    The two-storey complex sprawling over 16,000 square metres consists of over 22 retail outlets. Moreover, the RGO Welfare Shop, the anchor outlet at Al Burj is no newcomer to Muscat, and has been servicing people for over 10 years now. In the initial phase the shop’s reach was restricted to the RGO employees, but since 1996 it has been thrown open to the public and Al Burj Trading was incorporated in 1998. It has now metamorphosed to become one of the biggest hypermarkets in the country. Al Burj holds franchises for Subway, Hot Breads, in addition to franchises for fashion and jewellery stores which are multi-brand outlets specialising in ladies handbags, accessories, menswear and leather accessories. These stores represent well-known brands such as Pierre Cardin, Valentino Rudy, John Longford etc. Another major attraction here is Garant Mobel, the German household furniture brand.<br>
                    Moreover, Al Burj enjoys a captive customer base of RGO employees and has been popular among the local residents on account of its wide product range and value-for-money pricing policy. “We always add an element of excitement to shopping with our special offers and discounted prices,” says Mahrooqi. “The hypermarket strategy is to always have cheaper prices on everything. Our prices are the benchmark for the rest of Oman,” he adds.<br>
                    Being strategically placed at Seeb, just 15 minutes away from the airport Al Burj aims at tapping the one million residents of Greater Muscat and Batinah regions. “We intend to meet the total shopping needs of the entire family under one roof in a high quality shopping environment,” avers Mahrooqi. He also adds that they soon hope to become a destination mall not only for the local residents but for all in and around the region. The complex had a soft launch in November 2001 and initially relied on word of mouth publicity. “But now since the infrastructure is fully developed, we are targeting to attract around 4000 visitors every day, and have adopted an aggressive marketing strategy,” reveals Mahrooqi.<br>
                    <br>
                    <b>
                    Possibilities</b><br>
                    The Al Burj team does not seem too intimidated about the presence of City Centre which is just a 10 minute drive, or Markaz Al Bahja, another mega mall coming up soon in the same vicinity. “Since the population in and around Seeb and Batinah alone is about 800,000 and with Oman’s population steadily growing by two per cent per year, there is enough and more potential for us,” they say confidently.<br>
                    But not all share their confidence. “Markaz Al Bahja and the Lulu Hypermarket coming up at the nearby Al Ghoubra roundabout will definitely eat into Al Burj’s business,” opines an industry watcher, who further informs that it is true that Al Burj had the lowest prices earlier but now the prices of most products are at par with the rest of the supermarkets. The hypermarket operates 26 cash counters with a separate cash and carry outlet for the benefit of bulk purchasers. Around 90 per cent of the customers are Omani families from the lower-middle to middle income category.<br>
                    Sumithra Raju from the Perfume and Cosmetic Division of Muscat Pharmacy, one of the tenants of Al Burj says, “We chose to set up an outlet here because of the tremendous potential that this area offers. Sales have been good right from the beginning.”&nbsp;<br>
                    “Al Burj is at a locational disadvantage since it is off the highway and families would prefer to shop in the nearby City Centre,” insists an analyst who has keenly been studying the evolution of the shopping malls in Oman for the past 15 years.<br>
                    It is quite natural that Al Burj comes into comparison with City Centre the 44,000 square meters, RO25-million mall which opened around the same time, in almost the same locality, for both of them fall into the same category and they share the same catchment area. At the heart of the City Centre is Carrefour, the mall’s French anchor store and the world’s second largest hypermarket offering over 60,000 products. This mall also houses a leisure and entertainment zone called Magic Planet, a selection of restaurants and over 60 retail outlets including an additional six sub-anchor stores.&nbsp;<br>
                    However, Al Burj’s Mahrooqi says, “We don’t think of City Centre as competition. In fact we complement each other. Now Seeb is quickly replacing the traditional shopping pockets of Ruwi and Qurm, to become a sought after shopping destination because of our combined presence. Customers come to Seeb now because they can enjoy a great shopping experience in a relaxed atmosphere with an extensive variety and range to choose from.”<br>
                    Also, it is not just City Centre that Al Burj will have to contend with. For the customer, a neighbourhood retail shop would be the logical choice for the daily use items given its proximity. Of course then there are the likes of Lulu Shopping which offers immense price benefits, Sultan Centre, a stand alone supermarket with an excellent price and product range, the Al Fair chain of supermarkets and Al Jadeeda stores with their personalised services. One just has to wait and watch to see which one of them are the fittest and will survive the onslaught of competition.&nbsp;</font></p>
