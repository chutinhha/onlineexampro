		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b>Oman could meet its energy 
					demand through renewable sources and ship its non-renewable 
					fossil fuel to bring in revenues for the national economy</b></font><b><font face="Verdana" size="1"><br>
					</font>
					</b><font face="Verdana" size="1"><br>
					<b>Why would a country which derives approximately 50 per 
					cent of its GDP from the oil and gas sector invest in 
					technology that, ideally, will render hydrocarbons obsolete?</b><br>
					On first thought, this is counterintuitive. Countries which 
					derive a large portion of their revenues from hydrocarbon 
					exports hardly seem the most likely candidates to invest in 
					alternative sources of energy. Throughout the GCC, 
					hydrocarbons account for nearly 90 per cent of exports, and 
					therefore a substantial percentage of GDP. In Oman, the 
					figure is closer to 75 per cent, with only the UAE – with 53 
					per cent - exporting less. A drop in prices provoked by 
					falling demand would wreak havoc on government balance 
					sheets.<br>
					<br>
					Yet the GCC is emerging as a hotbed of renewable energy 
					research and development, and is predicted to become even 
					more invested in the nascent green sector. With a little 
					creative thinking, it is easy to see why.<br>
					<br>
					Most obviously, hydrocarbons are a finite resource and so it 
					makes sense to develop alternatives for when they run out. 
					This has been a concern for Oman, which faces the prospect 
					of running low on its oil reserves earlier than many of its 
					neighbours. Yet Oman is still a net hydrocarbon-exporting 
					nation, and can meet most domestic consumption needs for 
					many years to come – so why put such an emphasis on 
					developing alternative energy sources?<br>
					<br>
					A simple answer lies in the continuing high price of oil and 
					gas. Every barrel of oil which Oman uses at home is one less 
					that it can export at a profit. And while domestic energy 
					demand will continue to increase well into the future – 
					indeed, it is expected to double by 2021 – Oman could meet 
					much of it by tapping some other natural resources it 
					possesses in abundance and which are harder to package and 
					ship abroad – namely, sun and wind.<br>
					<br>
					<b>PROFIT OR LOSS?</b><br>
					The Sultanate is not a new player in the alternative energy 
					game: the Ministry of Water Resources erected wind turbines 
					in Oman as far back at 1996. Yet for years alternative 
					energy was viewed as a luxury, unlikely to turn a 
					conventional profit.<br>
					<br>
					This perception is changing. As the alternative energy 
					industry is still young, it is hard to estimate the costs 
					associated with a switch to solar and wind power. In the 
					short term, both renewables are much more expensive than 
					conventional gas and coal power. But depending on the pace 
					of development in the sector, that could change more quickly 
					than many expect. The price of photovoltaic (PV) solar 
					cells, which have been implemented on a large scale nearby 
					as part of Abu Dhabi’s Masdar Initiative, are expected to 
					fall 3-7 per cent annually for the foreseeable future, 
					according to a recent report by consultancy Booz &amp; Company. 
					If the price of carbon-based energy production remains the 
					same, solar PV could become cost-competitive with the 
					unsubsidised price of natural gas between 2015 and 2025.<br>
					<br>
					<b>SUBSIDY BARRIER</b><br>
					A key element of the preceding sentence is ‘unsubsidised’. 
					As part of its effort to guarantee all Omanis the best 
					standard of living possible, the government currently 
					subsidises the electricity market heavily: energy is sold to 
					the public at a fifth of the generation and transmission 
					cost. The availability of bountiful and cheap energy serves 
					as a disincentive to private investment in research and 
					development, which tends to have high upfront costs. To an 
					extent, this can be countered by increased government 
					investment. But an ideal market would see both public and 
					private sector funds contributing to the mix, which is 
					unlikely while the cost of electricity remains artificially 
					low.<br>
					<br>
					Subsidies have other downsides. Principally, they are likely 
					to become much more expensive in the coming years, as Oman’s 
					power and water needs continue to increase. Thanks to 
					industrial and infrastructure projects, electricity is 
					increasing by approximately 15 per cent a year, according to 
					Oman Power and Water Procurement Company (PWP) figures. Gas 
					demand as of mid-2009 was 13.6 million cubic metres per day 
					and will increase to 20 million by 2015. To meet current 
					demand, it imports gas from Qatar via the Dolphin Energy 
					pipeline at the highly advantageous rate of $1.35/mn btu. 
					However, with far eastern markets willing to pay up to $10/mn 
					btu, it is unlikely that Qatar will want to increase the 
					amount it sells to its neighbours.<br>
					<br>
					<b>Attractive alternatives</b><br>
					“Governments have to start accepting that the gas price will 
					have to be higher. They are going to have to be much higher 
					than US$5/mn btu,” Yousuf Al Ojaili, CEO of the state-owned 
					Oman Gas, told local press at the Gas Arabia Summit in Abu 
					Dhabi this February. With increased gas prices, alternative 
					energy sources will become increasingly cost-competitive. 
					But the incentives for developing solar and wind power are 
					not purely economical.<br>
					<br>
					Increasing awareness of the stress placed on the environment 
					by fossil fuel-based power generation has led to a flurry of 
					international treaties dedicated to reducing carbon output. 
					Oman, for its part, is a member of the UN Framework 
					Convention on Climate Change (UNFCCC) and a signatory of 
					numerous treaties devoted to environmental preservation. 
					Perhaps more important, however, is the public perception of 
					the downsides of burning cheap carbon-based fuels. According 
					to a new survey conducted by Accenture, nine out of every 
					ten consumers in the GCC want their country to reduce its 
					reliance on oil and gas. Sixty-seven per cent of respondents 
					think the way forward is through the development of ‘clean’ 
					technology.<br>
					<br>
					<b>Eco-concerns</b><br>
					Pollution and concern for long-term environmental impact are 
					cited as the chief concerns motivating this demand for 
					low-carbon energy. This is especially the case in Oman, 
					which has a rich history of environmental awareness: 
					environmental protection laws have been on the books since 
					1974, and it became the first Arab state to create a 
					ministry dedicated to environmental issues in 1984.<br>
					<br>
					Hydrocarbon extraction relies heavily on machines and 
					capital-intensive exploration. Development and installation 
					of alternative sources of energy requires investment in the 
					knowledge economy and, to a lesser extent, construction. 
					Cleaner sources of fuel will also help preserve Oman’s 
					environmental richness, which is vital to the growing 
					tourism industry. All three of these sectors contribute to 
					diversifying Oman’s economy, a key component of the Vision 
					2020.<br>
					<br>
					And while Oman cannot bottle up and export sun and wind, it 
					could sell the technology it develops. Both Western and 
					Eastern markets have proved themselves willing to spend on 
					green tech, with China recently surpassing the US in overall 
					investment, spending $34.6bn in 2009. The pace of 
					development in the sector will depend on incentives, 
					investment in research, and technology transfer, all of 
					which is hard to predict. However, while exact numbers may 
					be hard to pin down, it is clear that Oman stands to benefit 
					hugely from developing its alternative energy sector.
