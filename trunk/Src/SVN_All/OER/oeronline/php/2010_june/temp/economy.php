                    </font><b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <img src="images/Oliver_Cornock.jpg" hspace="9" vspace="5" border="0" align="right" width="65" height="110" style="border: 3px solid #EFEFE9; "></font><font face="Verdana" size="4" color="#C00000">Carving 
				an Advantage</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" size="2"><br>
				</font><font face="Verdana" size="2">India’s estimated $500bn 
				infrastructure investment requirements over the next three years 
				are an excellent opportunity for Oman to invest some of its 
				excess liquidity</font></b><p align="right" style="margin: 3px 10px" dir="ltr">
				<i><font face="Verdana" size="1" color="#0000FF">By Oliver 
				Cornock</font></i><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
				Global attention last year focused heavily on the deterioration 
				of international trade, as shrinking consumer demand in the West 
				hit manufacturers hard and the credit crunch curtailed the flow 
				of capital across borders. In the first quarter of 2009, 
				year-on-year global FDI flows halved, while mergers and 
				acquisitions activity practically ground to a halt – falling 
				from just under a quarter of a trillion dollars in Q1 2008 to a 
				little over $50bn in Q1 2009.<br>
				<br>
				<b>Bucking the trend<br>
				</b>And yet behind the headlines lie other stories, some of 
				which contradict the general flow of events. One bilateral 
				relationship to buck the trend was that between India and Oman, 
				which continued to grow from strength-to-strength last year 
				despite the global crisis. Non-oil trade between the two broke 
				the $2bn mark in 2009 – ten-fold growth since the turn of the 
				decade, when trade was less than $200m a year.<br>
				<br>
				While precise figures are yet to be released, it would seem that 
				despite a dip in merchandise trade, overall capital flows 
				between the two countries tipped the $2bn mark – exhibiting 
				steady growth on the previous year’s figures, and demonstrating 
				the growing importance of bilateral links between the two 
				economies. As early as August last year, India’s ambassador to 
				Oman, HE Anil Wadhwa, predicted that trade between the two would 
				reach $2.5bn for 2009, given that year-on-year figures for the 
				first two months of 2009 demonstrated a growth of 12 per cent.
				<br>
				<br>
				While that figure may prove to have been ambitious, the massive 
				growth in trade over the past decade speaks volumes for Oman’s 
				improved standing in terms of trade. India now represents Oman’s 
				second largest non-oil export market behind the UAE, with 
				exports hitting almost a quarter of a billion rials ($650mn) in 
				2008, while India is Oman’s fifth largest import market – the 
				Sultanate imported 400mn rials ($1.04bn) in goods from India the 
				same year.<br>
				<br>
				<b>Increasing investments<br>
				</b>Merchandise trade is one aspect of the economic relationship 
				between the two nations, yet arguably it is not the most 
				significant. Of perhaps greater long-term importance is the 
				increasing amount of investment being carried out between the 
				two countries, best exemplified by the establishment of the 
				India Oman Joint Investment Fund. The fund, which will have 
				initial capital of $100mn, was established initially by the 
				signing of an MoU in 2008 between India’s largest commercial 
				bank, the State Bank of India (SBI) and Oman’s sovereign wealth 
				fund, the State General Reserve Fund (SGRF).<br>
				<br>
				While modest, the fund’s capital can be increased to $1.5bn 
				either through taking on additional debt or attracting new 
				partners. Most significant though is the fact that the fund 
				represents the first such undertaking made by India with another 
				country – an indication of the growing strength of Omani-Indian 
				ties, and the preferred status of Oman as a partner for India 
				within the GCC. <br>
				<br>
				The fund, which is in the final stages of preparation before 
				being officially launched, follows on from previous joint 
				ventures such as the Oman India Fertiliser Company, whose 
				operating capital is shared between the Oman Oil Company (50 per 
				cent), the Indian Farmers Fertiliser Cooperative Ltd (25 per 
				cent), and Krishak Bharati Cooperative Ltd (25 per cent). 
				Meanwhile in India, Oman Oil Company is investing in a six 
				million tonne oil refinery in Madhya Pradesh through its 
				subsidiary, Bharat Oman Refineries Ltd. <br>
				<br>
				<b>Private capital<br>
				</b>Oman has proved to be a popular investment location for 
				private Indian capital as well. Speaking to local media last 
				year, HE Wadhwa listed several Indian companies demonstrating an 
				interest in investing in various sectors of the Omani economy. 
				Examples include L&amp;T, which signed an MoU to establish a heavy 
				engineering company in Sohar, while in the oil and gas sector a 
				consortium led by Reliance recently won the rights to explore 
				several of Oman’s oil blocks. <br>
				<br>
				In the services sector, Infoline is seeking tie-ups with Infosys 
				and Wipro, while in the hospitality sector Taj and Oberoi groups 
				are looking to enter the luxury hotel market. Finally, Indian 
				steel and mining giant the Tata Group has floated a subsidiary, 
				Reemal Mining Company, on a 70:30 equity basis with the Al Bahja 
				Group to mine limestone and gypsum in the Sultanate.<br>
				<br>
				Such partnerships are likely to prove mutually beneficial to 
				both Oman and India. On the one hand, India’s growing economy is 
				sprouting potential global players on a regular basis and on the 
				other, Oman, with its liberal investment environment and a 
				desire to attract new industrial capacity, represents for 
				companies an ideal seed market through which to enter the GCC, 
				and perhaps the wider MENA region.<br>
				<br>
				<b>Indian opportunities<br>
				</b>Key players such as the Oman Oil Company can find ample 
				opportunity to leverage their own capabilities in the Indian 
				market. What is certain is that India’s growth presents itself 
				as a much more benign phenomenon than that of, for example, 
				China.<br>
				<br>
				The world’s largest democracy can sometimes appear chaotic and 
				rambling in comparison with the steely discipline of its 
				northern neighbour; however, that is also a large element of 
				India’s strength in dealing with international partners. Its 
				engagement is never overbearing – it does not seek to monopolise 
				resources, nor does it seek to claim special favour on the basis 
				of the large number of expatriate Indian workers in the GCC. 
				Rather, it presses its case on its own merits – as a 
				competitive, dynamic emerging economy, with a strong desire to 
				increase trade among its neighbouring states in the Indian Ocean 
				rim.<br>
				<br>
				It is in this spirit that India hopes to double current levels 
				of bilateral trade with the Arab world over the next four years, 
				from $114bn at present. In particular, the Indian government is 
				seeking Arab investment in sectors such as shipbuilding, 
				infrastructure, pharmaceuticals, IT, agro-processing and energy. 
				While Oman, due to its size, has the physical capacity to lay 
				claim to only a small portion of that total, the Sultanate’s 
				efforts over the past decade ensure that it has an early 
				advantage in securing the best access to the Indian market.<br>
				<br>
				Ultimately, as the global trading environment continues to 
				recover, we are likely to witness the continued acceleration of 
				a trend, which was beginning to be felt prior to the credit 
				crunch in 2008. With growth likely to remain sluggish in the 
				OECD for the next few years, and the supposed low-risk element 
				of investing in such economies blown quite out of the water, the 
				Middle East’s sovereign wealth funds may well become 
				increasingly bullish regarding investment potential in the East.<br>
				<br>
				After all, the emerging markets of Asia increasingly require not 
				only raw inputs for manufacture, but also manufactured goods 
				themselves, as purchasing power increases and domestic consumer 
				demand grows. By establishing strong partnerships now, investors 
				such as Oman can benefit from this trend in the years to come.
