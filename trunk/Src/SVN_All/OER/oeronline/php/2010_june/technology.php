	<tr>
		        <td valign="top" width="100%"> 
				<p align="center" style="margin: 3px 10px"> 
					</p>
				<p align="justify" style="margin: 3px 10px" dir="ltr"> 
					<b><font face="Verdana" size="2"><br>
					While there is no scientific evidence about the adverse 
					affects of mobile phones on health, public concerns persist. 
					To dispel such notions it is imperative for the media, 
					mobile companies, government and regulators to work in 
					tandem<br>
					<br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/jun_technology_img1.jpg" hspace="9" vspace="5" style="border: 1px solid #C0C0C0; " width="144" height="177" align="right"></font></b><font face="Verdana" size="1">The 
					question of whether the media gives an accurate picture of 
					health issues associated with mobile phones belongs within a 
					wider area of research in the social sciences: media 
					reporting of scientific issues. Public opinion on mobiles 
					phones and health is greatly influenced by what they read, 
					hear and see in the media and an important parallel here is 
					the reporting of scientific research on global warming. To 
					put it succinctly, almost all scientists think global 
					warming is happening, yet the public think this is an area 
					in which expert opinion is evenly divided.<br>
					<br>
					The UN Intergovernmental Panel on Climate Change’s fourth 
					report in 2007 made its strongest ever recommendation 
					stating that global temperatures are rising and there is a 
					90 per cent or more possibility of this being caused by 
					human activity. So scientists agree there is a crying need 
					for more action to reduce emissions. And no national or 
					international scientific body has a dissenting opinion on 
					the topic. 32 national science academies back the UN view 
					and have called for reduction in greenhouse gases. On the 
					other hand, there are a tiny number of individual scientists 
					who question the prevailing view. <br>
					<br>
					To see how well the public understands scientific opinion on 
					global warming the World Bank in 2009 took a poll of 13,000 
					people in 15 nations. While 99 per cent of the scientists 
					think that the “problem is urgent and enough is known for 
					action”, the general public has a very different impression. 
					The survey showed that only in countries like Bangladesh and 
					Vietnam does the public have anything approaching the 
					correct understanding. Across the 15 countries on average 
					only 51 per cent of the public have a correct picture of 
					scientific opinion, and 39 per cent think this is an area in 
					which expert opinion is evenly divided or not enough is 
					known to justify action being taken.<br>
					<br>
					<b>Mobile phones and health</b><br>
					So what do the scientists say about the impact of mobile 
					phones on health? According to the World Health 
					Organisations’ (WHO) Factsheet 2000, “Current scientific 
					evidence indicates that exposure to RF fields, such as those 
					emitted by mobile phones and their base stations, is 
					unlikely to induce or promote cancers.” This premise is 
					embellished by other sources like the Stewart report like 
					the UK 2000, “…on the basis of the evidence currently 
					available, there is no need for the general population to be 
					worried about the use of mobile phones.” Health Canada 2003 
					states, “the weight of evidence from animal, cell culture 
					and human studies does not indicate that the energy emitted 
					by cell phones is strong enough to cause serious health 
					effects.” <br>
					<br>
					The public perception is far removed from such a standpoint. 
					In 2007 Eurobarometer carried out a survey which involved 
					face-to-face interviews with approximately 30,000 EU 
					citizens at their homes. The survey found that concerns 
					about mobile phones and health had increased since 2002. The 
					results showed considerable cultural variations. For example 
					people in Italy, Greece and Cyprus were most concerned while 
					in Sweden, Finland and Denmark they were least concerned. 
					Remarkably, there was little variation within the survey in 
					the responses of people due to their level of education or 
					between the sexes. Close to 76 per cent thought that mobile 
					phone masts affected their health “to a big extent” or “to 
					some extent”, while 63 per cent thought the same about 
					mobile phone handsets.<br>
					<br>
					Overall, around 48 per cent of the public was fairly 
					concerned about the implications of mobile phone handsets, 
					while 49 per cent were not very concerned or not concerned 
					at all. The depth of the misunderstanding is shown by 
					comparing public opinion on global warming - an issue backed 
					by confirmed scientific research and views on mobile phone 
					related health risks, where there is no confirmed research. 
					In the UK, around 52 per cent of the respondents were 
					concerned or very concerned about mobile handsets, while 65 
					per cent were concerned or very concerned about climate 
					change. <br>
					<br>
					The perceived risk has made this a common reason for the 
					public to complain to their elected representatives. In 2005 
					UK MPs like Ian Gibson stated that constituents raised 
					concerns about health risks from mobile phones and masts 
					‘incessantly.’ The constituents of Andrew Mitchell MP 
					Praised concerns ‘extensively.’ Andrew Stunell MP went on 
					record saying, “In the last four years, I’ve probably had 
					seven or eight communities send me petitions about mobile 
					phone technology.”<br>
					<br>
					<b>Scientific method<br>
					</b>In popular perception scientists are seen as finding 
					‘the answer’ or things that can be, ‘scientifically proven.’ 
					In fact scientists try to disprove the currently accepted 
					orthodoxy. Hypotheses are not ‘true’: they have merely 
					resisted falsification, while scientists who believe a 
					theory will often try their best to disprove a hypotheses. 
					When a theory is disproved a new scientific paradigm begins, 
					which researchers then test until its flaws are exposed. 
					Accepted theories rarely explain every conceivable 
					circumstance: science is far less certain than the public 
					imagine.<br>
					<br>
					The internet has changed the traditional relationship 
					between science and the media as it has empowered the public 
					to find things out for themselves. Self-diagnosis is a new 
					frontier for medicine but whether this is a blessing or a 
					curse remains unanswered. The internet also empowers small 
					groups to communicate with each other and it is a boost for 
					special interest lobbying. Much of internet content though 
					lacks the discipline of the traditional media like balance 
					and a separation of fact and comment. The growing importance 
					of search engines further clouds the picture – the best 
					researched and most balanced views doesn’t necessarily get 
					the most google hits! The power of the internet does as much 
					to spread false rumours as it does to empower the public. 
					Remember the video showing that you could use your mobile 
					phone to make popcorn? Nonsense, but it has become an urban 
					myth. It was in fact a viral marketing scam by Cardo, a 
					company making Bluetooth headsets for motorbikes and it got 
					nine million views in 180 days.<br>
					<br>
					As these rumours have the power to adversely affect public 
					perceptions, it is important for various constituents to 
					play their respective roles responsibly. The media needs to 
					give an independent critical assessment and an accurate 
					picture of scientific research and alert the public about 
					possible health risks. They must resist the temptation to 
					exaggerate and recognise that one experiment or an 
					individual experience does not add up to a confirmed 
					hypothesis. The government should protect the public, have a 
					legal framework based on scientific knowledge and stimulate 
					economic development. Regulators on their part need to 
					ensure compliance with regulations, stimulate competition, 
					improve services to public and stimulate economic 
					development. It is tempting for journalists to demonise the 
					commercial sector: in fact there are profound social 
					benefits from improving the communications infrastructure 
					and many members of the public, either personally or through 
					their pension funds are shareholders in mobile companies.<br>
					<br>
					Scientists on their part need to add to knowledge and public 
					safety cannot be guaranteed without their independent 
					assessment of possible risks. In fact, the public feel 
					poorly informed and want to know more, preferably on TV, 
					according to a recent Eurobarometer study. What can public 
					bodies do to address this lacunae? I would argue that 
					regulators and governments have the resources to influence 
					the debate, they can promote public protection while 
					emphasising that their risk assessment is based on the 
					latest scientific research. They should try to become the 
					public’s No.1 trusted source by promoting a balanced view 
					and challenge inaccurate and balanaced media reporting. They 
					should also make reliable and easy to understand information 
					available in print and online.<br>
					<br>
					<b>Consultation wins friends</b><br>
					Operators need to realise that applications used to erect 
					base stations are a key moment to win consumer confidence. 
					In the UK opposition to base stations is often driven by 
					poor public consultation, mobile companies are often 
					perceived as arrogant – assuming that no-one will object to 
					the mast. Many people believe that masts are a health risk: 
					so they think it is arrogant if operators try to install one 
					without trying to persuade them that it is safe. Failure to 
					persuade feeds into public distrust of governments and big 
					business while openness counteracts claims of conspiracy. 
					Operators wanting to put up base stations need to put 
					resources into convincing the public by attending or 
					convening public meetings, carrying out leaflet campaigns 
					and PR campaigns. The government and regulators need to 
					produce information packs for the public about base 
					stations. <br>
					<br>
					There should be media guidelines on such matters. New 
					research must be reported but journalists should make clear 
					where this research stands in relation to majority 
					scientific opinion, whether it is unpublished or published 
					(and therefore peer reviewed) and how much credence it is 
					likely to be given within the scientific community. Research 
					saying there is no health risk should be given similar 
					prominence to that which suggested there was a danger. The 
					language used by the media should also be well thought out. 
					Lines like “A new study proves…” does not make a claim 
					incontrovertible? Single studies are rarely conclusive. It 
					is better to say that the study ‘indicates’ or ‘suggests’ 
					rather than implying it is conclusive. <br>
					It is important to give journalists better training on 
					science issues as most journalists are arts graduates. 
					Science is not the same as risk assessment. Risk assessment 
					is usually done by governments on which they are advised by 
					national and international scientific bodies. It’s not just 
					a question of what the media say…it’s a question of what the 
					public understands. People don’t understand the science in 
					detail, they “are aware of the main themes or frameworks of 
					media coverage” and use these as “building blocks” to make 
					sense of an issue, according to a recent academic study in 
					this area.*<br>
					<br>
					Developing this idea, what framework have people taken from 
					media coverage of mobiles and health? I would argue that for 
					many members of the public it is the following: there is a 
					scientific debate about whether mobiles harm health, 
					therefore there is good reason to doubt claims that there is 
					no health risk. <br>
					<br>
					It is therefore important to provide the public with another 
					framework and this can come from friends, family or 
					education. For example the study of mobile phones and health 
					can be part of the health or science agenda in schools and 
					colleges. Regulators or government representatives can talk 
					to parents’ organisations, clubs, societies, workplace 
					organisations and trade associations about the issue. 
					Combined with a communications strategy by governments and 
					regulators, all these measures will go a long way in helping 
					the public form balanced assessments of mobile phone health 
					issues based on confirmed scientific research.<br>
					<br>
					*Hargreaves, Lewis and Spears (2003) Towards a better map: 
					science, the public and the media.</font></p>
