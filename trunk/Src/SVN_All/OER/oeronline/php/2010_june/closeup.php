                	<b> 
                    <font size="4" face="Verdana" color="#C00000"> 
                    GCC: Envisaging budgetary surpluses</font></b></p>
				<table border="0" cellpadding="0" cellspacing="0" width="100%">
					<tr>
						<td valign="top">
						<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr">
						<b><font face="Verdana" size="2">
						Thanks to firm oil prices, collectively Gulf Cooperation 
						Council (GCC) countries are expected to post a surplus 
						rather than a deficit in fiscal year 2010. The original 
						forecast called for shortage reflecting unease related 
						to lingering adverse effects of the global financial 
						crisis</font></b></td>
					</tr>
					</table>
				<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                    <font face="Verdana" size="1">On an average, oil income 
					accounts for three quarters of the total budgetary revenues 
					in GCC countries. Yet oil income comprises nearly 90 per 
					cent of treasury earnings in Kuwait, making it the most 
					dependent amongst GCC countries on the petroleum sector. As 
					such, changes in oil prices affect the level of income and 
					thus expenditures in GCC countries.<br>
					<br>
					By and large, GCC states prepared 2010 budgets using 
					relatively low average oil prices, ranging from $40 per 
					barrel in the case of Bahrain, $50 per barrel in Oman and 
					$55 per barrel in Qatar. Yet, average oil prices have been 
					hovering around $70 per barrel in the first quarter and are 
					projected to remain steady for the rest of 2010.s<br>
					<br>
					Not withstanding the significance of this, two GCC countries 
					elected not to publish assumed oil prices used in preparing 
					their budgets. Two GCC members namely Qatar and Kuwait start 
					their fiscal years in April rather than January, suggesting 
					that figures relating to deficit or surplus of GCC budgets 
					are not utterly confined to 2010.<br>
					<br>
					<b>A surplus</b><br>
					A report by Emirates Industrial Bank predicts the six-nation 
					GCC reverting the projected deficit of $2.9bn into $50bn 
					surplus. Together, GCC budgets posted surpluses of $19.6bn 
					in 2009 and a hefty $189bn in 2008. The extraordinary 
					surplus registered in 2008 partly reflects rising oil 
					prices, which reached a record $147 per barrel in July of 
					the same year.<br>
					<br>
					Yet, prices dropped sharply for several months following the 
					outbreak of the global financial crisis reflecting declining 
					confidence. In fact, oil prices dropped below $40 per barrel 
					towards end of 2008 and the start of 2009. This explains the 
					reason behind Bahrain’s decision of assuming average oil 
					price of $40 per barrel for fiscal years 2009 and 2010. To 
					be sure, Bahrain stands out within the GCC for preparing 
					budgets for two consecutive fiscal years. The programme has 
					its advantages and disadvantages like providing direction of 
					governmental spending and being far too long in a 
					fast-moving world, respectively.<br>
					<br>
					The same report puts GCC’s combined budgetary expenditure at 
					around $270bn in fiscal year 2010, up from $236bn in 2009. 
					This represents a notable growth of 14.4 per cent and 
					deserves commendation. A number of institutions like the IMF 
					and G-20 amongst others call for enhancing public sector 
					spending as a means of containing adverse effects of the 
					financial crisis.<br>
					<br>
					<b>Economic growth</b><br>
					In reality, government expenditures in GCC are sizable by 
					virtue of representing nearly 30 per cent of the combined 
					gross domestic product (GDP). This is only an average 
					figure, as the rate reaches as high as 40 per cent in some 
					GCC countries.<br>
					<br>
					What matters in this case is that higher actual oil prices 
					versus the projected ones provide GCC governments an ideal 
					opportunity to increase their spending levels, thereby 
					paving the way for firmer GDP growth rates. This in turn is 
					considered essential for addressing challenges like creating 
					job opportunities and meeting expectations of locals. GCC 
					nationals prefer jobs offering a reasonable salary together 
					with attractive working conditions and <br>
					hours of work.<br>
					<br>
					Private sector investors take the lead on the economic 
					direction from public sector spending. In other words, 
					stronger public sector spending serves the purpose of 
					encouraging private sector investors to follow suit.<br>
					<br>
					<b>Stronger spending plans</b><br>
					GCC countries have planned stronger spending for fiscal year 
					2010 on the back of gradual return of normalcy of economic 
					conditions following the global financial crisis. For 
					instance, projected expenditure in Saudi Arabia amounts to 
					$144bn, up by 14 per cent versus the originally planned 
					figure for fiscal year 2009.<br>
					<br>
					For good reasons, the authorities allocated some $70bn for 
					developmental projects in 2010, up by $10bn from the 
					previous fiscal year. Undoubtedly, this is a considerable 
					figure by virtue of accounting for 49 per cent of total 
					projected expenditures. Projects envisaged include expansion 
					of the road network and new power and water plants, 
					construction of numerous educational and training 
					institutes. <br>
					<br>
					For its part, Qatar projects expenditures of $32.4bn in 
					fiscal year 2010-2011 which commenced in April, up by nearly 
					a quarter. Yet, Oman has pegged government expenditure in 
					2010 at $18.7bn, showing a rise of 12 per cent. Bahrain 
					authorities also managed to secure parliamentary approval 
					for increasing spending thanks to a higher budgetary income 
					than what was originally projected. As mentioned earlier, 
					Bahrain assumed a low oil price of $40 per barrel for fiscal 
					years 2009 and 2010.<br>
					<br>
					<b>Inflationary threats</b><br>
					However, stronger spending carries with it the threat of 
					inflation. According to the IMF, all GCC countries notably 
					Qatar suffered from relatively high inflation rates in 2007 
					and 2008 partly due to solid public spending. The IMF put 
					Qatar’s inflation rate at 15 per cent in 2007 and 2008.<br>
					<br>
					Undoubtedly, return of inflationary pressures cannot be 
					ruled out, but comparison with 2007 and 2008 is possibly not 
					warranted. In those two years, the gWwlobal economy was 
					suffering from the phenomenon of inflationary pressures 
					partly due to declining value of the US dollar.<br>
					<br>
					GCC economies tend to suffer from the debacle of imported 
					inflation during the period of declining value of the dollar 
					coupled with rising values of other hard currencies such as 
					the Euro and Yen. This is the price GCC countries have to 
					pay for linking their currencies to the dollar. Kuwait is an 
					exception as it links its dinar to a basket of currencies 
					that include numerous currencies including the dollar.<br>
					<br>
					Nevertheless, the challenge nowadays is rejuvenating local 
					economies in order to register the highest possible economic 
					growth rates. This particularly critical time requires 
					steady public spending, so as to deal with fundamental 
					problems, namely addressing matters such as economic growth 
					and creation of job opportunities. </font></p>                </td>
