					<img border="0" src="images/jun_interview_img1.jpg" vspace="4" hspace="9" align="right" width="150" height="170"></font><font face="Verdana" size="2">The Tamkeen programmes of study through research and 
				partnerships with a wide variety of organisations and 
				institutions, facilitates Omani jobseekers success in the new 
				millennium. SHK. Mohammed bin Abdullah Alharthy, Chairman of 
				Knowledge Grid that manages the Tamkeen programmes talks to the 
				OER team<br>
				<br>
				</font></b><font face="Verdana" size="1"><b>Could you elaborate 
				on the programmes provided by Knowledge Grid?<br>
				</b>Knowledge Grid will provide job oriented training programmes 
				aimed at promoting life-long learning for the development and 
				empowerment of individuals, communities and nations in the new 
				millennium. Knowledge Grid currently offers TAMKEEN programmes; 
				a set of technical and non-technical training programmes that 
				focus on bridging the skills gap between standard education 
				outputs and industry talent requirements.<br>
				<b><br>
				Are these programmes recognised?<br>
				</b>All TAMKEEN programmes are conducted after the approval and 
				recognition of the Curriculum Development Department at the 
				Ministry of Manpower. TAMKEEN seeks to be a brand for excellence 
				through our programmes of study and research, and our institutes 
				partnerships with a wide variety of organisations and 
				institutions – Edexcel, Microsoft IT Academy, City &amp; Guilds, 
				ACCA, CompTIA, Cadd Centre and Chartered Institute of 
				Environmental Health. Candidates who hold one of our 
				certificates have attained an excellent form of skills 
				recognition – all certificates detail those skills that have 
				been assessed and which the candidates are competent in.<br>
				<b>&nbsp;<br>
				Which training institutes provide these programmes?<br>
				</b>At this stage training programmes are to be implemented 
				through TAMKEEN approved institutes. Currently three institutes 
				are approved to conduct TAMKEEN programmes; Polyglot Institute 
				for administration, finance and IT programmes, Rusayl Institute 
				and Sohar International Institute for technical programmes in 
				all streams. Knowledge Grid also wishes to invite other training 
				institutes to register and start benefiting from TAMKEEN 
				programmes. Selected institutes are best known for their work in 
				providing effective training solutions to different types of 
				industry.<br>
				<br>
				<b>Could you elaborate a little on the facilities at the 
				training institutes?<br>
				</b>TAMKEEN approved institutes’ classrooms and laboratories are 
				equipped with the latest means including projectors, large show 
				screens, audio and visual equipment and PCs for Languages, 
				Secretarial and Business Administration programmes. In addition, 
				institutes’ workshops include a wide variety of electrical and 
				mechanical tools, equipment and machinery, as well as specific 
				welding and instrumentation workshop setups. <br>
				<br>
				<b>What are the education and other requirements to seek 
				admission to these programmes?<br>
				</b>TAMKEEN comprises of a set of technical and non-technical 
				training programs that focus on bridging the skills gap between 
				standard education outputs and industry talent requirements. 
				Generally, candidates, from those with a high school diploma to 
				those who are university graduates, who pass an entry test and 
				demonstrate commitment and motivation during their placement 
				interview, can gain admission to the programme that they wish.<br>
				<br>
				<b>What employment opportunities are available after completing 
				the programmes?<br>
				</b>TAMKEEN seek to find ways for all Omani jobseekers to gain 
				opportunity and success in the new millennium. One of TAMKEEN’s 
				objectives is to build Omanis’ competencies so that they become 
				the optimal choice at the time of recruitment and raise the 
				quality of their competitiveness. Our graduates are treated as 
				specialised technicians in their particular fields. There is a 
				shortage of trained technicians and there are many opportunities 
				available to TAMKEEN graduates. Our programmes are developed 
				based on specific skills required by industry and are hence 
				highly job oriented, which means our graduates have very high 
				employability skills that are sought after by most companies.<br>
				<br>
				<b>Will there be an assessment and certificate awarded?<br>
				</b>A combination of practical and written assessments will be 
				applied to check that the candidate’s knowledge and performance 
				is in line with the programme outcomes. Upon passing these 
				assessments, a TAMKEEN certificate, issued by the institute and 
				attested by the Ministry of Manpower, is provided to successful 
				candidates. Other international certificates can be attained 
				after meeting the set criteria and passing further examinations.
