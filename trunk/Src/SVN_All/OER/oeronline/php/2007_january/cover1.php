				<tr>
					<td bgcolor="#E1E1E1" height="26">
					<p style="margin-left: 10px"><b>
					<font face="Verdana" size="1">By Jon Gorvett, 
					Energy Analyst, Oxford Business Group</font></b></td>
				</tr>
			</table>
			</div>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">Oman has come a long way since Sultan 
			Qaboos took the prescient decision many years ago to make natural 
			gas the key resource for powering the country’s energy future. 
			Indeed, with hydrocarbon fuel prices high across the board nowadays, 
			such a step may seem straightforward. Back then, natural gas still 
			occupied a very junior position in the calculations of most national 
			and international energy strategists.<br>
			<br>
			Oman too had relied on oil as its economic and energy mainstay, a 
			situation that had become a habit since IPC drilled the first dry 
			wells back in the late 1950s. Yet looking at the Sultanate’s energy 
			balances today, it is the role played by natural gas – and liquefied 
			natural gas (LNG) in particular – that is clearly the one to watch 
			between the two giant hydrocarbons.<br>
			<br>
			The reason for this is in the numbers. In 2005, Oman’s total 
			production of natural gas was 917.17 billion cubic feet (bcf) – some 
			7.4 per cent up on 2004’s total of 852.9 bcf. Yet, official figures 
			for this year show that such single digit growth is meagre, as the 
			first six months alone of 2006 show gas output up some 28 per cent 
			over that in the first six months of 2005.<br>
			<br>
			Comparing 2004 and 2005 again, exports of LNG jumped from 6.9 
			million tonnes (mt) to 7.06 mt, with these too set to surge in 2006 
			as production from a new train comes online. As an indicator of just 
			how much it might jump, demand for natural gas to be used in LNG 
			production was up by a third during the first half of this year, 
			compared to the first six months of 2005.<br>
			<b><br>
			Growing importance</b><br>
			So, while oil continues to be the bigger revenue earner, gas is 
			catching up fast and looks likely to reposition Oman with a leading 
			role in the global LNG market. <br>
			<br>
			As a sign of the changing times, Indonesia, the world’s top LNG 
			producer, was in talks earlier this year with Oman – and Qatar – for 
			purchasing around a million tonnes of LNG per year to help ease the 
			South East Asian nation’s growing domestic energy shortfall.<br>
			<br>
			This trend of growing gas stardom of the Sultanate is set to 
			continue for some time as Oman possesses enormous proven gas 
			reserves currently put at around 29 trillion cubic feet (tcf). This 
			is a major jump on the 12.3 tcf recorded back in 1993. The increase 
			is no accident either, of course, as the quantity has leapt thanks 
			to a rigorous campaign of exploration of Oman’s subterranean 
			treasure troves. <br>
			<br>
			In the spotlight currently are the Khazzan and Makarem gas fields, 
			which have great potential and are even thought by some to contain 
			an additional 20 tcf. Not surprisingly, the contest to develop them 
			and ink production-sharing agreements with Muscat has been a tough 
			one.<br>
			<br>
			The increased interest in gas has had its effect on revenues too, 
			with gas earning Oman RO 397 million in 2005, or 8.6 per cent of the 
			country’s total revenue. This was substantially up on the 6 per cent 
			in 2004.<br>
			<b><br>
			Fuelling the economy</b><br>
			This urge to explore has been no isolated incident, but comes as 
			part of a master plan – Vision 2020 – for boosting the Sultanate’s 
			economic diversification. The strategy is that Oman will use gas to 
			fuel industry and power generation, as well as making LNG and boost 
			export revenues.<br>
			Oman has already linked a number of new power generation plants to 
			gas pipelines, specially in Raysut and Sohar, with the Sohar Power 
			Company soaking up much of last year’s 19.7 per cent hike in demand 
			from the domestic gas grid.<br>
			<br>
			On the LNG front too, the Sultanate is making good progress. Oman 
			LNG, the Sultanate’s first LNG company, which began in 2000 with two 
			liquefaction trains, is set to augment capacity. While its two 
			trains together produce some 6.6 million tonnes of LNG per annum, 
			the third train, run by Qalhat LNG – in which Oman LNG has a 36.8 
			per cent stake – will add 3.3 million tonnes more. Currently, talks 
			are also on for setting up a fourth train, with India a possible 
			target both for exports and for investment in the new development.<br>
			<br>
			Oman’s LNG players have been pushing their product hard abroad too, 
			with Oman LNG exporting to Europe and the US, as well as the key Far 
			Eastern markets of Korea and Japan. One of the drawbacks in LNG is 
			of course its distribution, requiring specialist facilities and 
			transporters. Yet, Oman has bitten the bullet and invested in 
			developing the Oman Shipping Company (OSC), taking delivery of two 
			more tankers back in June and boosting its fleet to six. The target 
			is to have 10 vessels by 2009 and already a deal to buy another two 
			ships has been struck.<br>
			<br>
			At the same time, gas must continue to fulfil its historical role of 
			helping boost oil production via reinjection techniques. Indeed, for 
			enhanced oil recovery, the oil sector swallows a major amount of 
			Oman’s natural gas output. Some 8.5 million cubic metres per day of 
			associated gas currently go to the oil sector, with this demand 
			soaring some 50 per cent last year, as oil companies tried to 
			maintain or even boost output and take advantage of high 
			international prices.<br>
			<br>
			Knit all these demand strings together and they exert a strong pull. 
			The newly developing port facilities and industrial zone at Sohar, 
			for example, are expected to consume around 23 million cubic metres 
			(mcm) of gas a day by 2009, given the plans for petrochemical plants 
			and a number of other major industrial projects.<br>
			<b><br>
			Bright future</b><br>
			Overall, Oman is looking to at least double its output of gas by 
			2010 and to ensure its optimum utilisation it requires an efficient 
			and widespread distribution network. This is the responsibility of 
			the Oman Gas Company (OGC). Today, the OCC is working hard on 
			developing Oman’s pipeline network, expanding it from 900 km six 
			years ago to over 2,000 km today. The pipelines form a 
			transportation network between the Fahud, Sohar, Al Dakhiliah and Al 
			Batinah regions in the north, and between Saih-Rawl and Salalah in 
			the south. As a sign of the high standing the company has, it 
			recently negotiated a US$ 234.5 million financing deal with the Gulf 
			International Bank.<br>
			<br>
			Industry players now envisage a future mass of pipelines across the 
			southern Gulf, linking gas supplies both within Oman and across the 
			borders to neighbouring states.<br>
			<br>
			There are still many challenges for the development of Oman’s gas 
			and LNG sector though. One is the thorny question of the 
			attractiveness of gas exploration contracts to outside companies. 
			This connects to the pricing structure and the reduced charges that 
			Oman is offering domestic industries in order to encourage their 
			development. Gas companies argue that this makes exploration less 
			attractive for them and they would like to see a move towards 
			adoption of market prices.<br>
			<br>
			Nonetheless, there is no denying that the Omani LNG and gas sectors 
			are moving fast and expanding dramatically. With this matching the 
			global trend in energy – and the national trend in diversification 
			–Omani gas will certainly be a major factor in the global scene in 
			the years to come.</font></p>
