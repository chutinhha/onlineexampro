<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b><br>
<img border="0" src="wors2.jpg" align="left" width="100" height="63">Qantas 
accepts Macquarie takeover</b><br>
Macquarie Bank takes over Australia’s flagship airline Qantas for US 8.7 billion 
(A$11.1bn). Shareholders will now accept A$5.60 a share, 33 per cent higher than 
a month ago when takeover speculation began. In the consortium Airline Partners’ 
Australia, Macquarie will own about 15 per cent of the shares – with the 
Australian finance company Allco being the largest shareholder through several 
subsidiaries. The Texas Pacific Group will hold less than 15 per cent, while 
Canada’s Onex Corp owns 9 per cent, and other foreign investors take up the 
remaining 15 per cent. This is in keeping with the current ownership law, which 
dictates that an investor cannot own more than 25 per cent of the firm’s shares, 
while no foreign company can hold a majority stake. <br>
<br>
The new owners reassured the labour unions that the airline will not be broken 
up, and overseas and regional routes will not be hived off. Despite suffering 
from the steep increase in fuel prices in recent years, Qantas remains one of 
the few global carriers to return a profit.<br>
