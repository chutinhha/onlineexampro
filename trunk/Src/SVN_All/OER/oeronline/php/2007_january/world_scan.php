<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b><img border="0" src="wors3.jpg" align="left" width="44" height="40">Fleet 
modernisation worth US$2.6 trillion</b><br>
Airbus Global Market Forecast 2006 predicts that about 23,000 new passenger and 
freight aircraft, valued at US$2.6 trillion, will be needed by aircraft 
operators in the period between 2006 and 2025. This requirement has been worked 
out on the basis of 4.8 per cent average annual traffic growth in revenue 
passenger kilometres (RPKs). In real terms, it means two and half times 
passenger traffic growth – and 14,600 more aircraft to the world fleet. Freight 
tonne kilometres (FTKs) are forecast to increase by six per cent annually over 
the same period, generating the need for 3,580 more freighters.<br>
<br>
The forecast predicts a higher replacement rate of over 12,000 aircraft due to 
an increasing demand for higher fuel-efficient airliners – to help control 
airline operating costs plus environmental concerns. Long distance routes like 
trans-Pacific, Europe-Asia and trans-Atlantic will see an increase by 60 per 
cent. The hub cities will see their traffic nearly doubling over the next 10 
years. Since 2000, there has been a 30 per cent growth in the number of seat 
availability, thus encouraging new operators in the emerging economies.<br>
<br>
The forecast, the result of a detailed analysis of current driving force of air 
transport that quantifies airline demand over the next 20 years, says that more 
than 70 per cent of all aircraft delivered between 2006-2025 will be 
single-aisled (100 – 220 seats). This translates to more than 15,300 aircraft, 
or 42 per cent by value of all passenger aircraft delivery. The 2006 report 
includes the Commonwealth of Independent States (CIS) for the first time, and a 
number of dedicated regional carriers have been added to the existing list of 
more than 400 airlines.<br>
