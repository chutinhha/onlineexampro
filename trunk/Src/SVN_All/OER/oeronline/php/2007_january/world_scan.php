<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b><img border="0" src="wors.jpg" width="100" height="101" align="left">Erasable 
paper from Xerox</b><br>
A paper that self-erases content within 16-24 hours will certainly find a good 
number of supporters. Xerox research wing, in partnership with Palo Alto 
Research Center (PARC), is in the process of inventing such a paper. The 
advantages of self-erasing documents are obvious – the paper turns into a new 
sheet within hours and can be used quite a few times, thus reducing paper 
wastage and helping our environment; and keeping alive the affinity towards pen 
and paper doodling. <br>
<br>
Xerox has filed for patents on the ‘erasable paper’ Technology, and PARC 
researchers have developed a prototype printer which creates on-page content 
using a light bar. This bar provides a specific wavelength of light as the 
writing source. The content then fades naturally over time. Alternately, it can 
be immediately erased after exposure to heat. <br>
<br>
The good news for businesses and homes is money saved, as the PARC printers will 
not need new toner cartridges every couple of months. But everything depends on 
how secure is the fading out of the content.<br>
