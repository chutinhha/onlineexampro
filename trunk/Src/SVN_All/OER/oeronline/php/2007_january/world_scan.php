<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b><img border="0" src="wors1.jpg" align="left" width="100" height="154">DP 
World sells US port assets to AIG Global </b><br>
DP World, UAE-based giant port company, has sold its holdings to a unit of 
US-based company AIG Global Investment Group, ending the most contentious deal 
in the recent past. While the actual deal has been kept under wraps, the company 
admitted to gaining about US$750 million from the sale. DP World, controlled by 
the government of Dubai, sold terminal operations of six ports including New 
York/New Jersey and Philadelphia, cargo-handling activities in 16 eastern 
seaboard and the Gulf of Mexico ports, besides a passenger terminal in the New 
York City. AIG’s asset management unit – AIG Global Investment Group – will take 
control of the port operations.<br>
Acquired from Peninsular &amp; Oriental Holdings of Britain in early 2006, the U.S. 
ports are said to be now worth $7.6 billion. But the deal ran into trouble as 
the U.S. politicians, cutting across party lines, rigorously opposed the deal. 
They were of the view that Dubai’s ownership of American assets could be a 
potential threat to the country’s security, The protectionist measures laid down 
by the US has attracted mixed reactions from the investors based in the Middle 
East.<br>
