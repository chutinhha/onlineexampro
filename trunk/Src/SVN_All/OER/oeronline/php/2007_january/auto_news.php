        It’s uber fashionable and showstopping. We are talking about Kue, the 
		Kia crossover concept from Kia Motors Corporation which will make its 
		world premiere at the North American International Auto Show (NAIAS) in 
		Detroit this month. Kia released a preview sketch of the all-new ‘Kue’ 
		concept car last month, which employs a different approach to the 
		crossover utility vehicle segment by taking elements of what makes a CUV 
		to a new level, namely performance. Kue has been designed as a driver’s 
		vehicle. While possessing SUV characteristics and capabilities, the Kia 
		Kue focuses more on giving the driver an exhilarating driving experience 
		on the way to the home improvement centre than on how much it can haul.
		<br>
		The Kia Kue has a monocoque platform that sits on 22-inch tyres with a 
		wheelbase of more than 114 inches, packaged in an overall length of 186 
		inches. Though purely a concept, the Kia Kue does, however, hint at 
		future styling directions for Kia Motors. Watch this space for more 
		details on Kia Kue. <br>
