        <font face="Verdana" size="1">
        <b>As genuine as it gets</b><br>
		With a Mazda you can be sure that genuine spare parts are never going to 
		be a problem. Towell Auto Centre, the national dealers of Mazda have 
		always been constantly attempting to increase the availability of 
		genuine Mazda parts. Since its launch in the Sultanate, the availability 
		of MAZDA genuine parts all across Oman is as high as 98 per cent, and 
		first pick availability has been growing. All genuine parts are sold at 
		all their 12 branches across the Sultanate. <br>
