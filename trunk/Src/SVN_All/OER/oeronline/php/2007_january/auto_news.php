		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>Making safety a priority </b><br>
		General Motors opened a state-of-the-art rollover crash test facility 
		last month becoming the first North American automaker to integrate 
		in-house testing for the infrequent but potentially deadly rollover 
		crashes that claim 10,000 lives a year. GM’s goal for the $10-million 
		facility is to study ways to potentially reduce injuries and deaths in 
		rollover crashes by developing sensors for air bags that can help 
		protect occupants in a rollover and help to keep occupants from being 
		ejected. <br>
&nbsp;</font><hr color="#C0C0C0" width="60%" size="1">
		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>PS22 delivers on Egyptian tracks </b><br>
		Pirelli PS22 standard truck tyres have repeated their last year’s 
		performance with an extraordinary win in the Pharoah’s Rally. PS22s, 
		which are mounted on Italian vehicles in size 14.00 R20, made the 
		difference on a wide range of varying surfaces in the 2006 Pharaoh’s 
		Rally, one of the best known and most demanding desert races in the 
		rally-raid category. Team Motorsport Italia Eurotrakker, which relied on 
		PS22 finished the 2006 Rally of the Pharaohs in 11th place overall.<br>
&nbsp;</font><hr color="#C0C0C0" width="60%" size="1">
		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>
		<img border="0" src="autn3.jpg" align="left" hspace="5" vspace="2" width="191" height="131">SEAT 
		gets futuristic</b><br>
		SEAT presented a Leon car with the latest technological breakthroughs at 
		the recently held Intelligent Transportation Systems (ITS) Congress in 
		London. The technological innovations were devised specially by SEAT, 
		through acute research, to find new forms of driver and vehicle 
		interaction. SEAT’s prototype can adapt its communication strategy in 
		accordance with traffic conditions, the surroundings and the driver 
		himself. SEAT is developing this project in collaboration with the 
		European Union. The Leon prototype was presented keeping in mind real 
		life possibilities, which instigate accidents. For instance, when the 
		driver takes his eyes off the road, the vehicle senses danger and turns 
		on a warning light on the dashboard. The vehicle emits a warning sound 
		and sends vibrations through the steering wheel if it detects that the 
		driver is falling asleep. Such advancements have been achieved by the 
		SEAT team with the use of sensors that control the driver’s eye movement 
		and blink frequency, radars to detect objects, GPS and other specific 
		communication networks. <br>
&nbsp;</font><hr color="#C0C0C0" width="60%" size="1">
		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>
		<img border="0" src="autn5.jpg" width="190" height="79" align="left" hspace="5" vspace="2">Forester 
		leads the pack</b><br>
		For the first time in the 15-year history of the Used Car Safety Ratings 
		(UCSR) study, a ‘standout’ car was identified, with Subaru’s Forester 
		(1997-2002) rating as top performer. The 2006 UCSR studied more than 1.7 
		million crashes of 305 vehicles over a 17-year period before arriving at 
		the verdict. The UCSR is the world’s largest study of vehicle crashes 
		and is conducted by the Monash University Accident Research Centre and 
		supported by the RACV, the Transport Accident Commission (TAC), and 
		VicRoads, as well as other state and federal road authorities and 
		motoring clubs from Australia and New Zealand. Project Leader, Dr Stuart 
		Newstead, of Monash University, said, “This car is a standout performer 
		– it shows you can have off-road capability with excellent occupant 
		protection and low aggressivity.”<br>
&nbsp;</font><hr color="#C0C0C0" width="60%" size="1">
		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>The Scots’ Choice</b><br>
		The Association of Scottish Motoring Writers recently awarded the 
		Scottish Car of the Year 2006 to the Jaguar XK Coupe. Jaguar’s UK 
		Managing Director Geoff Cousins collected the ‘Car of the Year’ award 
		for the XK. The Jaguar XK Coupe also picked up the ‘Sporting Car of the 
		Year’ award. <br>
&nbsp;</font><hr color="#C0C0C0" width="60%" size="1">
		<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        <b>The Phantom arrives</b><br>
		It has finally happened. The ultimate in luxury is in town. The much 
		talked about elitist automobile brand Rolls Royce was launched in Oman 
		last month. Al Jenaibi Automobiles, the exclusive dealers for BMW and 
		MINI, have brought Rolls Royce to Oman with launch of the Phantom in Al 
		Bustan Palace hotel on December 10. The Phantom has been a topper where 
		class, stateliness and super luxury is concerned. Speaking about the 
		entry of Rolls Royce into Oman, Axel Obermuller, Managing Director for 
		Europe and Middle East of the brand said, “Oman is a growing market; one 
		that has a lot of potential. The Omani economy has been registering a 
		stable growth even if it is slow. We knew that opportunities are plenty 
		in this market and we had to be here.”<br>
		<br>
		The ultra-luxurious automobile Rolls Royce Phantom is an amalgamation of 
		modern technology and craftsmanship keeping intact the glamour of its 
		heritage. The handcrafted car which boasts of interiors that takes 16-18 
		hides to complete, exquisite cabinetry and fine wood veneers is powered 
		by a 6.5-litre V12 engine. This translates to remarkable power, thereby 
		adding to Rolls Royce’s superlatives as one of the most powerful and 
		fastest cars. A car that can be personalised to the T, the Phantom comes 
		with exemplary features that include plush interiors, LCD screens for 
		the backseat passengers, in-built umbrella and much more. The star 
		attraction definitely is the retractable Spirit of Ecstasy, the figurine 
		that is synonymous with the brand name. Bespoke models, or customised 
		cars, will require a minimum of four months for delivery. Al Jenaibi 
		Automobiles is equipped to handle after sales and troubleshooting 
		services for Rolls Royce.<br>
