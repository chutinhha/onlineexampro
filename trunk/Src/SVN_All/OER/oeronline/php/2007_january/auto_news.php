        <b>
		<font face="Verdana" size="1">
<br>
        </font>
		<font face="Verdana" size="5">
        This is NOT a race! </font></b><font face="Verdana" size="1">
        <br>
		<br>
		<b>Technological prowess was evident at the Bahrain F1 venue as the new 
		Audi S models and Ulrike Lemmin-Woolfrey took to the track for a test 
		drive</b></font><p style="margin-left: 10px; margin-right: 10px" align="center" dir="ltr">
        <img border="0" src="autn.jpg" width="400" height="256"><p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
        <font face="Verdana" size="1">
        Think German luxury saloons and by the third attempt you will definitely 
		come up with Audi. Think Bahrain and pretty quickly you will get to the 
		Formula One International Circuit. Put them both together and you have 
		the recipe for a great way of launching the new Audi S models to an 
		automobile crazed Middle Eastern public.<br>
		<br>
		The Audi Paddock Club invited media from around the Gulf to be the first 
		to test-drive their new S-models, to experience first-hand what these 
		cars can do. When we arrived at the Bahrain International Circuit, the 
		brand-new Audis were lined up waiting to be inspected and driven around 
		the circuit and through various driving challenges. The line-up 
		consisted of the Audi S4 and S4 Cabriolet, the Audi S6 and the top of 
		the line sports version of the A8, the Audi S8, freshly shipped over 
		from Germany with certified and highly experienced driving instructors 
		to be launched not only to media but also to VIPs, dealers and 
		enthusiasts over a three-week period.<br>
		<br>
		The S models all come with the legendary Quattro permanent four-wheel 
		drive as standard, giving the cars a high degree of cornering ability 
		and driving enjoyment. To test the manoeuvrability, our first challenge 
		was to drive the Audi S4 cabriolet through a slalom course set up 
		outside the International Circuit, and do it fast. The spaces between 
		the traffic cones was challengingly short, but the challenge was met 
		easily, in my case probably more due to the car’s abilities as compared 
		to the driver’s. <br>
		<b><br>
		Safety Features</b><br>
		Next up was the Audi S6 to show us the Audis’ anti-lock-brake system. 
		Our instructions were to drive as fast as we possibly could – and 
		considering the S-models do 0-60mph in less than six seconds, we 
		certainly could go fast! – towards an obstacle, brake as hard as we 
		could and steer the car to safety alongside the obstacle whilst leaving 
		the foot on the brake. Considering that usually it is impossible to 
		steer whilst braking, this was new territory, but successful achievers 
		were rewarded with the Audi’s hazard-lights coming on, a new safety 
		feature in the S-models. <br>
		<br>
		Having tested the two ‘smaller’ models, next was the beast in the 
		line-up, the truly impressive Audi S8 with its large metal grill, its 
		aluminium trim, mirror casings and lights, together with its 20-inch 
		cast aluminium wheels and four shiny oval tail pipes – the car oozes 
		power. This machine we were allowed to race around the circuit, although 
		only under strictest supervision and the many-a-times repeated warning 
		“This is not a race!” to over-enthusiastic members of the media. After a 
		few practice laps, we finally set off at high speed and that was when 
		the S8 showed its true colours: the engine could barely be heard and 
		although we screeched around tight corners, the car had us under control 
		and not only did we feel extremely safe, but also very comfortable.<br>
		<br>
		So, what’s with the understated luxury, but the sporty driving 
		experience? One motor journalist described the Audi S8 as a luxury 
		saloon with a hyperactive tiger in its tank, and the description cannot 
		be improved on, as although the power, especially of the S8, is obvious, 
		the car looks as if it should be taking executives to a board meeting 
		rather than being able to hold its own on a Formula One race track.<br>
		<b><br>
		Understated Elegance</b><br>
		But that is what makes Audi special – the cars are discreet and 
		understated yet to people in the know speak of un-pretentious elegance 
		and power, like wearing Armani as compared to Versace: you can tell it 
		is a very good product, but it does not smack you in the face with its 
		pretentiousness.<br>
		<br>
		And it is not only the outward elegance and sporty engine, but also the 
		interior that certainly exudes luxury. Apart from the exquisite leather 
		upholstery; the carbon fibre inlays; the three-spoke sporty steering 
		wheel; there are also the seats itself which adjust in every direction, 
		including up, ideal if you are not the tallest person. Then there is the 
		music system, something no car can do without. But in true Audi 
		S8-style, the music system is not any old CD player and radio, it is 
		Bang &amp; Olufsen, the distinctive Danish manufacturer of superior music 
		and media systems, making the music system not just a system, but a 14 
		loudspeaker surround experience. To adjust your music, you simply use 
		the central controller, which comes up with something not unlike a 
		pie-chart on the computer screen; letting you choose your options before 
		giving you further choices of adjustments. There are plenty of buttons, 
		but unlike in some of the other German car models which shall remain 
		un-named here, and which have been widely criticised by motor 
		journalists, the Audi’s MMI – or multimedia interface – is overseeable 
		and relatively easy to use.<br>
		<br>
		The Audi S-class is a remarkable fusion of Audi’s legendary sports cars, 
		with the Quattro – first introduced in 1980 - as standard; the aluminium 
		body and accessories reminiscent of the first Audi race car built back 
		early last century; and the German reliability, quality and luxury that 
		people have come to expect. And on top of it all you have the “Vorsprung 
		durch Technik”, Progress through Technology, which is Audi’s slogan and 
		ace in the pack. If you cannot be persuaded to buy an Audi just by the 
		sheer elegance and luxury of its ex- and interior alone, then test-drive 
		it and let the technology convince you. Only you will be hard-pushed to 
		find as excellent a test-drive location as the Bahrain International 
		Circuit. <br>
		<br>
		The day after the launch I was still dreaming of hurtling along the race 
		track in the S8, but instead was stuck in rush-hour traffic in my own 
		car, which is not German, not comfortable and, alas, does not have 450 
		horse-power to its name, but one can dream. And one day, when I do own 
		an Audi, then it will be a race! <br>
