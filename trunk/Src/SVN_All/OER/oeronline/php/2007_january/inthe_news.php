Gateway to Development</font></span></p>

<p align="justify" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<b>Experts opine that US-Oman FTA has opened a flood of opportunity for the 
Sultanate’s economic development. Akshay Bhatnagar looks at some of the 
strategies to maximise the benefits</b></font></p>
<p align="center" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<img border="0" src="inthenews.jpg" width="400" height="307"></font></p>
<p align="justify" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
“The Free Trade Agreement between Oman and the United States has the potential 
to usher in a new era. Apart from eliminating tariff barriers, the agreement 
will provide new opportunities in services, a secure and predictable legal 
framework for investment, and streamlined customs procedures to facilitate 
trade.” <br>
<br>
This statement by HE Gary A. Grappo, the US Ambassador to the Sultanate, paints 
a bright picture about the benefits that will accrue to Oman post-FTA. Shaun 
Donnelly, Assistant US Trade Representative for Europe and Middle East, echoed 
the sentiment, when he said: “For Omani businessmen, the agreement gives access 
to the world’s single largest market and provides boundless opportunities.”<br>
<br>
Together, these comments should help allay any concerns over the Free Trade 
Agreement (FTA), which was signed in 2006, especially among private sector and 
small and medium enterprises. The US-Oman FTA, HE Maqbool Bin Ali Bin Sultan, 
Minister of Commerce and Industry said, is currently being fine-tuned and is 
expected to come into effect early 2007. The FTA is a step in the direction laid 
down by the seventh Five-Year Economic Development Plan (2006-2010), which lays 
emphasis on enhancing the private sector’s role in the Sultanate’s growth and on 
increasing foreign trade and investment. To fulfil the twin objectives, Oman is 
already pursuing FTAs with various trading blocks, including the European Union, 
Turkey, China, Japan, India, Pakistan and New Zealand. <br>
<b><br>
New Frontiers</b><br>
To create awareness about the benefits of the FTA and to discuss opportunities 
for businessmen in both the countries, the Ministry of Commerce and Industry 
along with Omani Centre for Investment Promotion and Export Development (OCIPED), 
Oman Chamber of Commerce &amp; Industry (OCCI) and the US Embassy, organised a 
day-long ‘FTA Awareness Conference 2006’ at Muscat last month. More than 400 
senior representatives of the Sultanate’s corporate sector attended the event. 
More such awareness conferences are slated in other parts of the Sultanate in 
2007. <br>
<br>
Once the agreement comes into force, trade in consumer and industrial products 
will become duty-free. “Oman can export many products, including petrochemicals, 
handicrafts and fisheries. Similarly, we will be interested in US investment 
here to develop tourism, resorts, construction, IT and management,” HE Eng. 
Salem bin Said Al-Ghattami, Chairman of OCCI, said at the conference. <br>
<br>
On the benefits of the FTA, Grappo said, “The Agreement puts investors’ minds at 
ease through substantive legal protections, including the right to equal 
treatment and due process, the prohibition of placing certain restrictive 
requirements on investors, and the establishment of an effective, impartial, and 
transparent dispute settlement process. The Agreement also commits each 
government to establish high levels of environmental protection and to ensure 
that labour standards are consistent with internationally recognised labour 
rights.”<br>
<b><br>
Positive Impact</b><br>
So far only four Arab nations – Oman, Morocco, Bahrain and Jordan – have signed 
an FTA with the US. As a result, their bilateral trade jumped by 16.3 per cent 
between 2004 and 2005, and an estimated 33.5 per cent between 2005 and 2006. The 
example of Jordan is quite striking. In 2001, when the US-Jordan FTA was signed, 
the trade between the two nations was to the tune of only US$300 million. Since 
then it has grown to US$2 billion in favour of Jordan.<br>
<br>
Even though the US-Oman FTA is yet to come into force, a positive impact on the 
Sultanate’s economy is already evident. Trade between the US and Oman was worth 
about US$1.1 billion in 2005. But since then, till October 2006, US imports rose 
to about US$747 million, while Oman’s exports touched US$935 million.<br>
<br>
The Omani government has initiated a number of economic diversification projects 
in which US companies are actively involved. Dow Chemicals has teamed up with 
the Sultanate to build a petrochemicals complex to serve as an anchor for the 
development of Sohar. Bechtel Corporation is constructing the first greenfield 
smelter in the region in over 25 years for Sohar Aluminium Company, part of an 
US$2.2-billion project. Occidental Petroleum, along with its project partners, 
is investing more than US$3 billion over the next several years in the 
development of the Mukhaizna field.<br>
<br>
US corporations such as AES and PSEG have actively participated in the 
Sultanate’s privatisation of power and water generation facilities, while 
Parsons is helping in Oman’s transportation infrastructure development. US 
construction and financial firms, such as Turner Construction and Bear Stearns, 
are also actively participating in development resort complexes in Oman.<br>
<br>
Oman’s corporate sector too made a start when an agreement was signed between 
the OCCI and the National US-Arab Chamber of Commerce (NUSACC) to counsel and 
facilitate interaction between the Omani and US investors.<br>
<b><br>
Areas of Concern</b><br>
Though the Minister of Commerce and Industry said that Omani companies should 
not worry as special care has been taken to protect and promote them, especially 
with regard to the small sector, certain sections of the Omani corporate sector 
are still a bit worried. Omantel CEO, Dr. Mohammed Bin Ali Al-Wohaibi, raised 
the concern that Omani ICT sector companies were not in a position to compete in 
the US market as they were comparatively young. They would be at a disadvantage 
in the US market despite the FTA, he pointed out. <br>
<br>
The US Ambassador countered the argument. “The Omani companies will not be 
competing with giants such as Microsoft or AT&amp;T. They need to identify their 
niche areas and look for relevant opportunities with partners of the big US 
companies,” he said. David Hamod, President &amp; CEO of NUSACC also disagreed with 
Al-Wohaibi. “The small and medium (SME) enterprises have fewer overheads and 
hence can be very competitive. Our experience says that if we (Arab companies) 
do our homework well, we can do well,” he said.<br>
<b><br>
The Way Ahead</b><br>
John M. Adams, Senior Associate, Booz Allen Hamilton Inc., pointed at the way 
forward for Omani companies and the government. “The first task is to identify 
the key target sectors that have possible opportunities for increased trade,” he 
suggested, adding, “The sector-specific initiatives to improve sector 
competitiveness can be private sector-driven but will also require government 
policy support.”<br>
<br>
“Oman is at the crossroads,” he said, “some hard decisions must be taken. Payoff 
will not be immediate. Take for example, the barriers in sectors that provide 
the key inputs affecting the competitiveness of other sectors – such as ICT 
services and transport – should be reduced. While saying that “Oman is well on 
the way to creating a robust telecommunication market,” he pointed at the 
“comparatively high” cost of leased line. “An E-3 international digital leased 
circuit (including tail ends) from Oman to Europe costs more than US$250,000 per 
month; in Europe, on an average, the same capacity costs nearly half as much!” 
he said.<br>
OCCI Chairman Al-Ghattami agreed with Adams. “To enlarge the private sector’s 
benefits from the FTA with US, we have to work quickly to prepare detailed 
studies of the opportunities which this agreement provides at all fields and 
sectors besides identifying the suitable mechanisms and forms to achieve those 
benefits in the coming period,” he asserted.<br>
<br>
On the role of private sector and government in making FTA a success story, he 
said, “The FTA requires a common effort from both the government and private 
sectors to promote for the advantages which this Agreement is offering to the 
private sector. This would increase the private sector’s contribution in the 
comprehensive development plans and programmes including the plans of economy 
diversification, employment of people, attracting investments, transfer the new 
technology and develop the methods and tools of the management, production and 
marketing.”</font></p>
