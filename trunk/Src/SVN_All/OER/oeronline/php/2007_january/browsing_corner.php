            <br>
            <img border="0" src="browsing_corner1.jpg" width="180" height="284" align="right">As 
			our world gets transformed by the Internet, Chris Anderson in ‘The 
			Long Tail’ explains how endless choice is creating unlimited demand 
			across almost all industries What happens when there is almost 
			unlimited choice? When everything becomes available to everyone? And 
			when the combined value of the millions of items that sell in small 
			quantities equals or even exceeds the value of a handful of 
			bestsellers?&quot;<br>
			<br>
			In The Long Tail, Chris Anderson, the Editor-in-Chief of Wired 
			magazine, propagates that the future of business does not lie in 
			hits – the high-volume end of a traditional demand curve – but in 
			what used to be regarded as misses – the endlessly long tail of that 
			same curve. As the world gets transformed by the Internet and the 
			near-infinite choice it offers to consumers, traditional business 
			models are being overturned and new truths revealed about what 
			consumers want and how they want to get it. The world of books has 
			been transformed by Amazon and the record business by iTunes and 
			Rhapsody. A similar transformation is perceivable in just about 
			every industry imaginable. Wherever you look, modest sellers, niche 
			products and quirky titles are becoming an immensely powerful 
			cumulative force. <br>
			<br>
			Anderson first explored the ‘long tail’ in an article in Wired that 
			has become one of the most influential business essays of our time. 
			Now, through this book, he takes a closer look at the new economics 
			of the Internet age, showing where business is going and exploring 
			the huge opportunities that exist – for new producers, new e-tailers, 
			and new tastemakers. He demonstrates how long tail economics apply 
			to industries ranging from the toy business to advertising to 
			kitchen appliances. He sets down the rules for operating in a long 
			tail economy and provides a glimpse of a future that’s almost here.<br>
			<br>
			“Read this brilliant and timely book if you want to get a look at 
			the future of business,” says Eric Schmidt, CEO of Google, about The 
			Long Tail. The sentiment is echoed by Terry Semel, CEO of Yahoo!: 
			“Technology and the Internet are making the world a smaller and more 
			connected place. The Long Tail is the first book to explain exactly 
			how the ability to reach niche markets creates big opportunities.” 
			To know more about the book, log on to
			<a href="http://www.longtail.com">www.longtail.com</a>. <br>
