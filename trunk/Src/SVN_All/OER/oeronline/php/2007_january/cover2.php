					<p style="margin: 5px 10px" align="justify">
					<font face="Verdana" size="1"><b>Increased 
					investor confidence, industrial ventures, sound policies and 
					the continued strength of the international oil economy 
					together combine for a good year ahead for Oman’s economy<br>
					<br>
					</b>By Salem Ben Nasser Al Ismaily, CEO, The Omani Centre 
					for Investment Promotion &amp; Export Development (OCIPED)</font></td>
				</tr>
			</table>
			</div>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">The Arab and Gulf region in general 
			has recorded a positive growth benefiting from very strong global 
			commodity prices, including oil. The real GDP growth rates in the 
			range of 5 to 6 per cent appear likely to continue at least through 
			2007.<br>
			<br>
			The non-oil-exporting economies in the region are also growing 
			strongly this year and may reasonably be expected to continue with 
			similar performance in 2007. However, countries such as Somalia and 
			the Sudan continue to be shadowed by potential conflict and economic 
			embargos despite huge prospects for economic growth.<br>
			<b><br>
			Working together</b><br>
			The Arab world, unfortunately, is still struggling to appear on the 
			international investors’ radar screen. Countries that have achieved 
			some success in promoting foreign investment are those that have 
			managed to work as bloc in a more effective way. The most notable 
			economic bloc in the Arab World is the Gulf Cooperation Council (GCC).<br>
			<br>
			The GCC countries have adapted to the challenges to face competition 
			effectively and have emerged as a strong and prosperous bloc. With 
			global energy demand at high levels, the era of cheap oil seems to 
			be over at least in the foreseeable medium term. This has enabled 
			GCC countries to generate considerable fiscal surplus, improve 
			balance of payments drastically and make GCC the fastest growing 
			bloc in terms of GDP.<br>
			<br>
			The per capita income of GCC countries is amongst the highest in the 
			region today. What has changed the face of GCC during the last two 
			decades is the long-term vision, policy initiatives to diversify the 
			economy and bring down oil revenue proportion, and prudent 
			deployment of fiscal surpluses in multi-sector development.<br>
			<br>
			With increasing degree of openness in investment, trade and commerce 
			policies, rapid pace of modernisation and infrastructure 
			development, expansion of private sector role, GCC economic 
			transformation has been a role model. On the social front too, all 
			important indicators such as healthcare, literacy rate, and women 
			participation in governance indicate steady improvement resulting in 
			significant socio-cultural transformation.<br>
			<b><br>
			Positive outlook</b><br>
			The outlook for Oman remains positive, buoyed by continued strength 
			of international oil prices, which are set to keep export revenue 
			and fiscal earnings high, more than offsetting the impact of weak 
			oil production. Progress with a range of gas-based industrial 
			projects (including a new liquefied natural gas facility) also 
			brightens the Sultanate’s prospects, marking the advancement of the 
			country’s diversification programme and creating some of the jobs 
			urgently required by Oman’s rapidly growing labour force.<br>
			<br>
			The new industrial ventures seen in Sohar, Sur and other regions of 
			the country contain a large portion of foreign investment and mostly 
			private investment. More ventures are anticipated in the tourism 
			sector in 2007 and these ventures also will be funded by foreign 
			investment. This is a good indicator of foreign investors’ 
			confidence in the Sultanate of Oman.<br>
			<br>
			Local and foreign private sector investment was robust in 2006. Most 
			of the investment will continue to be in gas-based industrial 
			projects as well as in infrastructure to support the tourism sector. 
			Economic growth is not only supported by new infrastructure projects 
			but also consumption and relatively low interest rates. Growing 
			consumer demand has led to an increase in local production 
			capacities resulting in additional investment in manufacturing and 
			service sectors.<br>
			<br>
			Trade volume within the Arab world and Oman in 2007 is also expected 
			to rise as the result of the Free Trade Agreement that has been in 
			effect since 2005 between some of the Arab countries, and to which 
			Oman is a party. Net exports will be strengthened substantially, as 
			the new LNG capacity comes on stream and Oman India Fertilizer 
			continues its production at full capacity.<br>
			<b><br>
			Invest within</b><br>
			Conscious effort needs to be taken to promote investment within the 
			region rather than out of region. Deeper economic integration, 
			higher intra-Arab trade, legal and financial cooperation is needed 
			to strengthen the private sector’s role in economic development.<br>
			<br>
			Banks and investment houses have an important role to play in 
			increasing financial cooperation. They must come together to fight 
			financial crimes, channel investments across the region prudently, 
			syndicate towards development financing, and closely integrate trade 
			and commerce.<br>
			<br>
			It is essential for the Sultanate of Oman to continue in its efforts 
			to diversify its economy and integrate globally. For this, the 
			government should continue to enhance the rule of law, continue with 
			good fiscal and monetary policies, adopt procedures and systems that 
			provide good business climate and reduce the size of the government.</font></p>
