					<p style="margin: 5px 10px" align="justify">
					<font face="Verdana" size="1"><b>Provision 
					of tourism infrastructure and building of human resources 
					are two key action areas<br>
					</b><br>
					By Tim Redman, COO, Oman Tourism Development Company</font></td>
				</tr>
			</table>
			</div>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Vision 2007 </b><br>
			Tourism is a fairly new industry in Oman and expanding the sector 
			requires establishing ground rules, correct processes and best 
			practices which will take place over the coming years. I would like 
			to see Oman more actively marketed in the international community as 
			a preferred destination for the astute traveler. In the light of 
			increased development in the tourism sector, and the Ministry of 
			Tourism’s efforts to place Oman on the International tourist map, we 
			foresee many new opportunities in the New Year and ahead for 
			investors to take advantage of major infrastructural projects that 
			are vital to the future of the tourism sector in Oman.<br>
			<br>
			Oman’s tourism vision has always been geared towards quality rather 
			than quantity. The Sultanate offers a world class diverse and varied 
			tourism product that is targeted at high-net worth tourists. The 
			drive behind this strategy comes from ensuring that while tourism 
			reaps its economical benefits, its negative aspects are controlled 
			with minimal impact to the environment and culture through a small 
			footprint. </font></p>
			<p align="center" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">
					<img border="0" src="cover4.jpg" width="400" height="336"></font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">
					<b>Mega projects and their cascading effects</b><br>
			We have a number of projects with Joint Venture Partners such as 
			Sama Dubai, Orascom through their Omani company Muriya, Saraya from 
			Jordan and Qatari Diar amongst others. Our partners are chosen for 
			their strategic expertise as well as their investment capacity and 
			this allows us to derive the much sought after positive benefits of 
			organic growth, by proxy. This has enabled us as a start-up company 
			to achieve project value of over RO 1.5 billion within just nine 
			months.<br>
			<br>
			Each of our Joint Venture Partners in Oman has similar values, 
			beliefs, vision, mission and culture including Omanisation policies 
			and procedures. This, coupled with the strong support we receive 
			from Government, has created numerous job opportunities for Omanis 
			directly in our sector and will shortly have a significant positive 
			impact on the downstream employment in sectors of construction, 
			tourism related operations and the hospitality industry at large.
			<br>
			<br>
			In terms of the National Economy we feel these projects will fulfil 
			our primary mandate from Government to diversify GDP into the 
			tourism industry. As a major generator of foreign direct investment 
			(FDI), the projects will also have a positive spin off for many of 
			the service industries down stream of us such as realtors, banks, 
			insurance companies and others.<br>
			<b><br>
			Targeting MICE segment</b><br>
			Many of the projects are currently under design development and it 
			would not be prudent to talk about them at present but projects, 
			which we are busy with, include Al Inshirah and Oman Convention 
			Center.<br>
			<b><br>
			Project Components include:</b><br>
			Convention / Conference facilities – capacity up to 7,000 seats<br>
			Hotel accommodation<br>
			5 star - 250 rooms<br>
			4 star - 300 rooms<br>
			3 star – 300 rooms<br>
			Serviced hotel apartments – 150 rooms<br>
			<b><br>
			Office park</b><br>
			These projects will significantly increase the influx of visitors to 
			Oman, not only in quantity but diversity of profile and origin.<br>
			The primary function of the convention centre is to establish Muscat 
			as a specialist destination for MICE (Meetings, Incentives, 
			Conferences and Exhibitions) in the MENA region. The MICE industry 
			is currently one of the fastest growing segments of the tourism 
			market. Providing a skilled labour force for each of these projects 
			is a human resource challenge that is already being tackled in Oman, 
			by the National Hospitality Institute and Oman Tourism College.<br>
			<b><br>
			Both eco-friendly and sustainable</b><br>
			Yes, all the projects we are undertaking are environmentally 
			friendly. We have a project execution handbook, which ensures that 
			we work within the current government legislation and international 
			best practice. All our projects will have independent environmental 
			consultants from conception of the feasibility studies. Their main 
			role will be to develop environmental project plans that take into 
			consideration the physical, historic and social impact of each 
			project as well as the on-going management of the completed product.<br>
			<br>
			Of course, we see ourselves developing the infrastructure for 
			tourism in Oman and vital to that is working hand in hand with the 
			Ministry of Tourism to ensure that the type of tourism being 
			cultivated is sustainable and measurable. Our key role will be to 
			integrate sustainability within the physical and architectural 
			framework of our projects. Luckily, there are many case studies 
			around the world to learn from, with the help of our consultants we 
			can clearly identify the pitfalls and overcome them with innovative 
			best practice that respects the environment, culture and heritage of 
			each project site. <br>
			<b><br>
			Teething troubles </b><br>
			The primary hurdles to development are human resource skills. The 
			MENA region as well as other global markets are experiencing 
			unprecedented growth in this sector which has driven the demand for 
			skilled resource to record levels. Oman as an emerging player has 
			first to establish its credibility as a player in order to attract 
			the correct skills. Given the relatively low base we are coming off, 
			establishing ourselves as a competitive employer in terms of 
			opportunity, risk and reward is a significant challenge.<br>
			<br>
			Current infrastructure and support services to the industry such as 
			property legislation and related processes need time to mature to be 
			able to handle the industry specific requirements that tourism 
			development brings. Forward planning of major infrastructural 
			entities such as the airport and roads has been outstanding and 
			delivery looks set to match our delivery timelines so the outlook is 
			positive.</font></p>
