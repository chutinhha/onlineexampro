		        <td valign="top" width="100%" align="justify"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Branding the 
					future</font></b></p>
					<p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
					<p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/nov_interview1.jpg" width="200" height="264" align="left" style="border: 1px solid #C0C0C0"></p>
					<p style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b>HH Sayyid Faisal al Turki, COO of Brand Oman Management Unit 
					speaks about youth entrepreneurship, innovation, branding 
					and urban development. Excerpts from an interview:<br>
					</b></font><br>
					<font face="Verdana" size="1"><br>
					<b>Urban development is on this year’s Muscat Youth Summit 
					agenda. How will globalisation impact on our cities<font color="#000080">?</font></b><br>
					<br>
					In simple terms, globalisation is being driven by mass 
					communications. For example, today’s communications makes it 
					possible for a Manchester-based bank to deal with its 
					customers through a call centre in Mumbai, or for a Parisian 
					sportswear company to design its products in Lyon, make them 
					in Hanoi and sell them in Boston.<br>
					<br>
					Mass communications have made even the farthest-flung place 
					in the world just a mobile phone call away. The Pulitzer 
					Prize-winning foreign affairs columnist Thomas L Friedman 
					famously said ‘the world is flat’ – that’s so true these 
					days.<br>
					<br>
					Globalisation can be seen as a positive, negative or even 
					marginal process. And regardless of whether it works for 
					good or ill, globalisation is here to stay and it goes 
					without saying that every Omani village, town and city is 
					affected by it. Indeed, it’s shaping Oman’s economy, 
					business community, education system, healthcare, retail, 
					entertainment and cuisine. It’s driving structural change 
					and influencing the sultanate’s demographics – more people 
					than ever are now living in urban areas. In fact, 
					globalisation is making cities right across the world more 
					important as hubs of economic activity. And from a local 
					perspective, globalisation will also make some Omani cities 
					more important than others in driving the sultanate’s 
					growth.<br>
					<br>
					Urban development and the environment is another issue of 
					concern and will be tabled for discussion at the Summit. 
					Clearly the world needs to reduce its carbon footprint, and 
					cities – as incubators for change – offer a variety of 
					environmentally sensitive strategies for transport and how 
					people can live or conduct their business. Many cities 
					around the world are reducing their reliance on the car but 
					already cities consume 75 per cent of the world’s energy 
					supply and contribute an almost equal amount of the world’s 
					carbon emissions, so a small change in environmental 
					policies and behaviour in cities can have a dramatic effect 
					on the planet’s well-being.<br>
					<br>
					We also need to look to other cities for inspiration. For 
					example, congestion charging in New York wouldn’t have 
					worked if London hadn’t shown the way. Mexico City’s 
					Metrobús is an outstanding success that was inspired by 
					Bogota’s Transmilenio. What will 
					cities learn from how London hosts the 2012 Olympics, how 
					they’ve invested in public spaces and regenerated sections 
					of the capital’s famous East End?<font color="#000080"><b><br>
					</b></font><br>
					<b>How important is it that we brand 
					our cities and work in partnership with other cities?</b><br>
					<br>
					The urban environment is something that affects all of us. 
					Cities are where most of us live, work and socialise.
					What kind of urban environment do 
					we want to live in? Are those who live in some remote and 
					soulless suburbs people like us? Do we want to drive 
					everyday for two hours in traffic to an office in a central 
					business district? Do we want to shop in a local and 
					family-run corner shop that offers us local produce or in a 
					vast hypermarket where our every need can be met in a 
					sanitised safe environment?<br>
					<br>
					These days more and more people want to live and work in 
					lively mixed-use, multi-cultural cities, bustling with life, 
					full of independent businesses and creative people. Places 
					that have an authenticity that comes from a deep history 
					rather than from the architect’s office. Somewhere where you 
					can meet people from every corner of the world and walk of 
					life but where you can be anonymous if you wish. We’ll be 
					challenging the Muscat Youth Summit participants to tell us 
					what kind of city they want to live, work and play in.<br>
					<br>
					On attracting business and travellers, let’s be honest. 
					Whether you live in Muscat, Mumbai, Manchester or Melbourne, 
					every city has a brand. Having said that, only a few cities 
					actively pursue forming their brand to reflect how they want 
					to be perceived. Just flick through the pages of any 
					international business magazine and you’ll see how some 
					cities are going about this – branding themselves as: clean, 
					green, safe, fun, historic, business-friendly, welcoming, 
					beautiful, friendly, relaxing and modern.<br>
					<br>
					I’m a great believer in the fact that a city’s design, 
					planning, development and branding reflects its values and 
					those of its residents. Moreover, the culture and structure 
					of a city are shaped by the experience, aspirations and 
					desires of its citizens. These aren’t things that can be 
					invented – to be successful a city’s brand has to describe 
					it – not prescribe how it should be.<br>
					<br>
					But the built environment alone doesn’t define a city: 
					perception is crucial and attitude can be everything when it 
					comes to attracting talent, travellers and urban investment. 
					In simple terms, the way in which Oman’s cities are branded 
					and built has a profound impact on the people who live there 
					and the ones we’re looking to attract.<br>
					<br>
					There’s no silver bullet or magic formula to becoming a 
					successful city. Instead, successful cities are built on a 
					distinctive economic and social history, physical 
					environment and geography, as well as links with nearby 
					cities, social and economic connections that help them adapt 
					to the changing global economy.<br>
					<br>
					For example, Muscat’s relationship and proximity to other 
					regional and international cities has a significant 
					influence on its opportunities for economic development and 
					sustainability.<br>
					<br>
					We hear a lot about the knowledge economy and the importance 
					of innovation and entrepreneurship – topics that were 
					discussed at Brand Oman’s recent ‘Inside Stories’ seminar. 
					It’s also another Muscat Youth Summit topic.
					What’s your take on youth 
					entrepreneurship and encouraging small business development?<br>
					<br>
					In my view, youth entrepreneurship is intrinsically linked 
					to national competitiveness, an issue Brand Oman is 
					concerned with. In this regard, and in partnership with, 
					UNESCO and the Ministry of Education we run the annual 
					Muscat Youth Summit.<br>
					<br>
					This year’s three-day residential programme will host a 
					series of workshops on youth entrepreneurship, sessions 
					delivered by representatives from The Prince’s Youth 
					Business International, the Commonwealth Secretariat, Injaz, 
					The Young Foundation, Intilaaqah and Knowledge Horizon. It’s 
					through this type of exposure that we’re looking to 
					encourage Summit participants – aged 15 to 24 - to become 
					entrepreneurs or entrepreneurial thinkers. Hopefully, we’ll 
					achieve this by immersing them in real life learning 
					experiences where they can take risks, manage the results 
					and learn from the outcomes. It’s a step in the right 
					direction.<br>
					<br>
					Perhaps people don’t realise the size of the world’s youth 
					population and its importance to the global economy. The 
					World Bank estimates that by 2015 – target year for the 
					Millennium Development Goals – there’ll be 3 billion people 
					in the world under the age of 25.<br>
					<br>
					Encouraging and facilitating new business formation is one 
					of the most important economic and social activities that 
					Oman can be involved in. We recognise that start-ups, small 
					and medium-sized firms are growing in importance. They 
					create the all important new jobs; contribute to the 
					nation’s GDP; produce innovations; account for a large 
					portion of sales; and employ large numbers of high tech 
					workers - such as scientists, engineers and computer 
					workers. Entrepreneurship isn’t something special that a few 
					people are born with, it’s a way of thinking that can be 
					nurtured – this is what we want Summit’s participants to 
					understand. Indeed, it’s a culture we’re looking to promote 
					amongst Oman’s youth. <br>
