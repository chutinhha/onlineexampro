                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">What next for 
					emerging markets?<br>
					</font><font face="Verdana" size="2"><br>
					The year 2011 could be different for Russia, Turkey, India 
					and Thailand who were highly profitable macro traders in 
					2010<br>
&nbsp;</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">Emerging markets have been the 
					world’s favourite asset class at a time when the dollar is 
					toast, commodities are king and Western economic growth is 
					burdened by excessive debt and sluggish bank credit growth. 
					A rising tide lifts all boats. However, it is now prudent to 
					cherry-pick and even prune exposure to individual emerging 
					markets. Russia, Turkey, India and Thailand were all highly 
					profitable macro trades in 2010 but 2011 could be different.<br>
					<br>
					Russian equities are vulnerable to a collapse in oil prices 
					and the mood swings of global investors. However, Russia is 
					the cheapest BRIC market, with a price/earnings ratio that 
					is less than eight times, a fraction of India’s 19 times 
					2011 forward valuation multiple. Yet oil lags gold or 
					agriculture commodities and OPEC quota discipline is still 
					enough to sustain a $70-85 trading range on crude oil. 
					Unless the US slips into a double dip recession, crude oil 
					and the Russian rouble will not collapse, the market’s 
					nightmare in 1998 and 2008. The world’s fund managers are 
					still grossly underweighing Russia at a time when corporate 
					earnings growth is at least 25 per cent next year. The 
					Kremlin has borrowed money in the Eurobond market for the 
					first time since the Yeltsin rouble debt default in August 
					1998 and President Medvedev is determined to attract FDI via 
					judicial reform. Russian oil companies Rosneft and Lukoil 
					are among the cheapest natural resource companies on the 
					planet. Russia’s de facto banking champion Sberbank can well 
					deliver 50 per cent EPS growth even as it trades at less 
					than two times book value. An alternative is the Russian ETF 
					listed in New York (symbol RSX), which has 30 per cent 
					upside next year and can be bought on any correction.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Turkish delight</b></font><font face="Verdana" size="1"><br>
					<br>
					Turkey has been among the most popular emerging markets 
					among fund managers since the trauma of 2008. Istanbul’s 
					ISE-100 index has more than doubled since it bottomed in 
					March 2009. Economic growth momentum and industrial 
					production both suggests that Turkey will continue to 
					attract hot money interested in investing in high growth, 
					domestic demand oriented emerging markets. However, 
					political risk is rising in Turkey as the Erdogan government 
					faces an election in March 2011 even as Kurdish secessionist 
					rebels in eastern Anatolia escalate violent attacks on 
					Turkish military garrisons. The next Turkish election is 
					significant because it is the first election since 2002 
					where the AKP could well lose its huge single party 
					majority. This event could well trigger an offshore money 
					exodus from the Istanbul stock exchange. The AKP could well 
					succumb to the temptation for pre election populist 
					spending. Turkish valuations are no longer dirt cheap at 2.2 
					times book value and 10 times earnings, a significant 
					premium to Russia. However, the easy money has been made in 
					Turkish equities.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>Book 
					profits</b></font><font face="Verdana" size="1"><br>
					<br>
					India is the most expensive emerging market in the world, at 
					four times 2011 book value and 19 times forward earnings. 
					Hot money (FII in polite company) inflows total $10bn in 
					September and October alone. This is boosted by stock index 
					arbitrage (long cash/short single scrip futures). However, 
					Dalal Street is now exposed to FII outflows to a far greater 
					degree than during the market peak in January 2008. The 
					Sensex is now in the danger zone though new FII inflows 
					could well take us to 22,000 by year end. However, I would 
					sell Indian equities on any rallies. The offshore liquidity 
					tidal wave depended on a rising rupee but the RBI has made 
					no secret of its determination to intervene. This scenario 
					will trigger a short, sharp correction in Indian equities.
					<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					India lures</b></font><font face="Verdana" size="1"><br>
					<br>
					Domestic funds have begun to take profits in India, 
					particularly in second tier, illiquid or high beta shares. 
					PSU banks, consumer staples (Nestle at 40 times earnings), 
					capital goods producers are unjustifiably expensive. It is 
					premature to short India as long as global risk appetite 
					remains robust and the Fed, ECB or major world central banks 
					do not raise interest rates. However, hot money driven 
					market bull runs eventually end in tears. Let investors not 
					forget the lesson of 2008-9, when the Sensex plummeted from 
					21,000 to 8,000 in the post-Lehman global panic though the 
					Indian economy did not go into recession. Hot money, not 
					economic or political climate, determines India’s valuation.<br>
					<br>
					Thailand was Southeast Asia’s Cinderella market until the 
					May 2010 military crackdown on pro-Thaksin red shirt 
					protestors on the streets of Bangkok. Yet political risk in 
					Thailand has not prevented a 30 per cent summer rally in the 
					SET index. The Thai baht has regained its 1997 level against 
					the dollar. I think Thailand could well be the winner Asian 
					emerging market of 2011 as the rally has been powered by 
					local, not foreign money. The world’s fund manger universe 
					is still underweight Thailand, haunted by memories of past 
					capital controls, coup d’état and political violence. After 
					all, the SET index plunged by 19 per cent in December 2006 
					when Bangkok imposed capital controls to combat baht 
					strength.<br>
					<br>
					Thailand is cheap at 12 times earnings and offers 18-20 per 
					cent earnings growth next year and GDP growth could well 
					accelerate to nine per cent. Thai refiner PTT and Bangkok 
					Bank could well lead a bull run in 2011.<br>
