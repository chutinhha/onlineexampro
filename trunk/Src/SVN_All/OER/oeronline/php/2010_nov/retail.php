		        <td valign="top" width="100%"> 
                <p align="justify" style="margin: 3px 10px" dir="ltr"><b>
				<font face="Verdana" size="4" color="#C00000"> 
                    <br>
				Social Conscience</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<b>
				<font face="Verdana" size="2"><br>
				<br>
				<img border="0" src="images/nov_retail.jpg" width="200" height="273" align="left" hspace="7" style="border: 2px solid #C0C0C0; ">Clive Freeman, Territory Head, Landmark Group, Oman speaks about 
				the recently launched Beat Diabetes campaign and the upcoming 
				walkathon to Mayank Singh<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				</font><font face="Verdana" size="1" color="#000080"><b>The 
				Landmark Group has announced the launch of its Beat Diabetes 
				initiative in Oman. What are the activities that have been 
				planned as a part of the initiative?</b></font><font face="Verdana" size="1"><br>
				<br>
				The Beat Diabetes campaign was launched in 2009 by the Landmark 
				Group in the UAE as a walkathon. As a part of the walkathon, 
				testing stations were set up on the way and a large media 
				campaign about the growing incidence of diabetes took place. As 
				we are one of the largest retailing chains in the region, we 
				have the ability to talk to a lot of people and the board of the 
				Landmark group decided to focus on this one concern. From media 
				reports and government officials we found that diabetes is a 
				growing health hazard in the region and decided to launch this 
				campaign.<br>
				<br>
				As a first step in a series of initiatives, we are organising a 
				2.1 kms walkathon on November 12, 2010, starting and ending at 
				Centrepoint - Ruwi. As a part of the campaign we will offer free 
				testing and information on how to prevent and combat diabetes. A 
				large number of people who are afflicted by type 2 diabetes are 
				not even aware of the disease and so this is an opportunity for 
				them to get themselves checked. Prevention is better than cure 
				and if detected early, diabetes can be managed much more easily.
				<br>
				<br>
				<font color="#000080"><b>Has the Beat Diabetes campaign been 
				launched in other parts of the region?</b></font><br>
				<br>
				It was launched in the UAE last year and this year we are 
				launching it in five countries – Kuwait, Qatar, Bahrain, Oman 
				and India. We are working very closely with the Ministry of 
				Health in Oman. The World Diabetes Day is on November 14 and so 
				all activities are planned around it. <br>
				<br>
				<font color="#000080"><b>What kind of help are you getting from 
				Oman’s Ministry of Health? </b></font><br>
				<br>
				When we approached the Ministry of Health they were fully 
				supportive of the initiative. The ministry has done a lot of 
				work on diabetes and they fully appreciate the need for 
				education and to raise awareness about the disease. According to 
				their figures 10 years ago 8.5 per cent of Oman’s population 
				suffered from diabetes. The unofficial figure is that 15 per 
				cent of the people in Oman suffer from diabetes now. If you take 
				into account the fact that there has been a rise in the 
				population, then the actual number of people suffering from the 
				disease has become quite high. Type 2 Diabetes is mainly a 
				lifestyle disease and major contributing causes are poor eating 
				habits and lack of exercise. <br>
				<br>
				<font color="#000080"><b>How will you create awareness about the 
				campaign and the disease?</b></font><br>
				<br>
				Apart from the walkathon we will be speaking to the media. In 
				addition, we will be creating awareness through our own stores 
				and with the help of our partners we will be educating people 
				throughout the year. We are keen to tie up with like-minded 
				businesses, to create mobile testing facilities. Though we are 
				starting off in Muscat we will be making these Oman-wide 
				campaigns similar to the the Landmark group’s Pan Arab campaign. 
				At the walkathon we expect several thousand people to turn up 
				and some local celebrities will also join in. Registration forms 
				for the walkathon are available at the customer service counters 
				in all Landmark group stores in Oman, and customers can register 
				themselves. On registering themselves participants will get a 
				registration ID sticker. On the day of the walkathon they can 
				present this sticker and get the kit (T-shirts and caps). We 
				would ask people to read the available literature about 
				diabetes, listen to the advice of medical professionals and work 
				towards reducing the incidence of the disease. The testing will 
				be done by accredited medical professionals. In the UAE 7,000 
				people have already been tested since 2009 free of charge.<br>
				<br>
				<font color="#000080"><b>Most businesses have been impacted to 
				some extent by the global financial crisis. How have you fared 
				in the last couple of years?</b></font><br>
				<br>
				It has been a challenging time throughout the world but in Oman 
				the Landmark Group has weathered the storm well. We offer a 
				range of goods that is difficult to match by any other retailer 
				and these are offered at very competitive prices. Our buyers and 
				merchandisers based in Dubai work very hard on getting the right 
				products at the right time.<br>
				<br>
				<font color="#000080"><b>Cu</b></font><b><font color="#000080">stomers 
				became a bit cautious about spending in the last couple of 
				months, did you find the same trend in retailing as well? </font>
				</b><br>
				<br>
				Over the last two years consumers have become more cautious in 
				their spending habits, particularly when purchasing large ticket 
				items. Retailers generally have responded by creating more 
				promotions and offers in an attempt to stimulate sales. The 
				Landmark Group has always worked on a philosophy of the widest 
				range of product at highly competitive prices and this has 
				helped us in good stand through the crises. This year we are 
				beginning to see increased footfalls in the business, however 
				some caution still remain.<br>
				<br>
				<font color="#000080"><b>Did you change the way you did business 
				to combat the slowdown? </b></font><br>
				<br>
				Like all businesses we have revised our operations to improve 
				efficiency, however this has not held us back in our plans. As a 
				group we are always looking to expand and will continue to do 
				so.<br>
				<br>
				On the product side we have excellent team who are always 
				sourcing and developing new products for our business. We listen 
				to our customer needs, source and deliver the product to our 
				stores and are always striving to move forward. We never stand 
				still and are always developing areas of our business to improve 
				product offering, customer service and operational efficiencies.<br>
				<br>
				<font color="#000080"><b>Supply chain management is an important 
				part of the retailing business. How do you manage this key 
				component? </b></font><br>
				<br>
				We have just signed a deal to build a 25,000 sq mtr warehouse. 
				Thus we are bringing in new modern warehousing systems. Oman is 
				quite challenging because of the low population of the country 
				in comparison to its geographical size. A town like Salalah is a 
				1,000 kms away from Muscat and so logistics becomes a key part 
				of one’s business. Our new facility will bring in modern 
				logistics facilities which will see us through the next 15 
				years. We expect the facility to be up and running in the next 
				18 months. <br>
				<br>
