				<b><font face="Verdana" size="2">
				<img border="0" src="images/nov_browsingcorner1.jpg" width="200" height="365" align="right"></font></b><b><font face="Verdana" size="2">Hamish McRae’s What Works captures the common thread that runs 
				through 20 success stories from across the globe.</font><font face="Monotype Corsiva" size="2"> </font></b>
				<font face="Monotype Corsiva" color="#808080">
				A review by Muhammed NafiE</font><b><font face="Verdana" size="2"><br>
				<br>
				</font></b><font face="Verdana" size="1"><b>Does Germany’s 
				industrial powerhouse Mittelsand have any thing in common with 
				Dharavi, Asia’s largest slum in Mumbai made famous by Slumdog 
				Millionaire? What are the common characteristics displayed by 
				the thriving Edinburgh Fringe Festival in Ireland and China’s 
				emerging financial capital Shanghai? Or what binds International 
				Baccalaureate (IB), the world’s fastest growing school 
				curriculum and examination system, to the bumpy desert boom in 
				Dubai?<br>
				</b><br>
				Despite their seemingly different exteriors, they have some 
				unique traits which make them one of the 20 success stories in 
				the world as featured in award-winning journalist Hamish McRae’s 
				What Works, a counterintuitive exploration of some of the most 
				successful communities and organisations from across the globe. 
				The book is a collection of experimental essays looking into the 
				flimsy difference between triumph and failure.<br>
				<br>
				From each experiment he tries to deduce some principles behind 
				success and examines how some initiatives took off while others 
				floundered and how some communities and organisations managed to 
				achieve so much while others struggle. Instead of taking a look 
				at the way the world is changing from a macroeconomic 
				perspective, the book illustrates, through a series of stories, 
				how great and innovative ideas can bring about change. McRae 
				does not advocate a unique model of development for the whole 
				world nor does he believe that one successful idea can be 
				replicated or transplanted in another side of the earth. But 
				drawing lessons from eye-opening ideas from every continent-from 
				America to Europe, from Africa to Asia and Australia- he 
				suggests to learn a bit from each of these stories and tries to 
				apply some elements of their success. “Good ideas will 
				increasingly come from anywhere and everywhere. We need to learn 
				from each other,” says McRae.<br>
				<br>
				He proposes some powerful ideas that can be applied to many 
				situations and, if sensitively and thoughtfully applied, can 
				bring about better outcomes. He captures some of the common 
				thread that run through all these narratives such as optimism 
				balanced by realism, excellence tempered by decency, becoming a 
				true magnet for talent, honesty about failure, the need for 
				humility, nimbleness, careful management of resources and having 
				a sense of mission. Though many of his examples such as Ireland, 
				the city of London&nbsp; and property development in Dubai have been 
				adversely affected by the world financial crisis, McRae argues 
				that, in an apologetic vein, their fundamentals have not changed 
				at all.<br>
				<br>
				However, he is not merely glorifying the triumphs but juxtaposes 
				the achievements of each setting with its flipside. For 
				instance, while attributing Tokyo’s remarkably low crime rate to 
				the koban – manned police cabins – dotted throughout the city, 
				he admits the fact that the yakuza, the organised crime 
				syndicates of Japan, have not been controlled. Likewise, he 
				observes how Australia is still faced with an obesity epidemic, 
				notwithstanding its excellent sports training facilities and 
				athletics prowess.<br>
				<br>
				Written in a lucid style without jargon or clichés, the book 
				sketches a broader global perspective for growth and development 
				and proposes indigenously developed mechanisms as a corrective 
				to the current economic gloom. </font><b>
