		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    For a green cause</font><font size="2" face="Verdana"><br>
				</font>
				</b>
				<b><font face="Verdana" size="1">
				<br>
				</font></b>
				<font face="Verdana" size="1"> 
                    <img border="0" src="images/nov_environment1.jpg" style="border: 2px solid #C0C0C0" align="right" hspace="9" vspace="4" width="200" height="255"></font><b><font face="Verdana" size="2">HSBC 
				Climate Partnership aims to combat the urgent threat of climate 
				change by inspiring action by individuals, businesses and 
				governments worldwide, reports Muhammed Nafie<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<b>Adnan Haroon</b>, HSBC Oman’s Financial Controller, has 
				recently been to the lush green Witham Woods in the heart of 
				Upper Thames in Oxfordshire to attend an employee engagement 
				programme on climate change as part of HSBC climate partnership. 
				Having spent a week among a team of scientists and volunteers 
				from Earthwatch and visiting the pristine English forests to 
				examine the long term impact of a changing climate on forest 
				ecosystems, he came back armed with the very latest scientific 
				information about climate change and the ideas and inspiration 
				to tackle it. In a candid interview to OER, Haroon explained the 
				objectives and goals of HSBC’s climate partnership programme and 
				its importance in formulating scientific and well-planned 
				measures to understand and counter the repercussions of climate 
				change on our planet earth.<br>
				<br>
				A $100mn, five-year programme, the HSBC Climate Partnership 
				brings together HSBC, Earthwatch Institute, The Climate Group, 
				Smithsonian Tropical Research Institute and WWF to tackle the 
				urgent threat of climate change on people, water, forests and 
				cities. Started in 2007, the partnership focuses on creating 
				cleaner and greener cities, protecting world’s major rivers and 
				the livelihoods of people, conducting field experiments on the 
				long-term effects of climate change and creating ‘climate 
				champions’ worldwide who will undertake research and bring back 
				valuable knowledge and experience to their workplace and 
				communities.<br>
				<br>
				“The partnership is not all about the money that we give to the 
				four international NGOs to implement the projects; but it’s a 
				broader five-year commitment wherein more than 2,000 selected 
				employees will visit and participate in the activities including 
				discussions with scientists and action planning sessions to 
				further their understanding of climate change,” says Haroon. 
				“Back to their work place, these people can develop projects 
				that further HSBC’s commitment to sustainability.”<br>
				<br>
				Haroon says creating awareness is very important. HSBC operates 
				in more than 80 countries and territories and has over 100 
				million customers; if 2,000 employees across the globe coming 
				back from the workshop could meet 300,000 employees and 100 
				million customers, the message can be conveyed to a great number 
				of people. It can bring about a huge change. “No organisation 
				can address the world’s problems alone but we believe HSBC has 
				an important part to play. With around 9,500 offices worldwide, 
				small change can make a big difference,” he says.<br>
				<br>
				Earthwatch is working with HSBC to help their employees learn 
				more about climate change, what it means to the business and 
				what they can do to combat it. Its five-year global programme of 
				forest research focuses on the impact of climate change on 
				temperate and tropical forests. “In five regional climate 
				centres set up in China, India, Latin America, North America and 
				Europe, scientists, supported by local community members and 
				HSBC employees, will carry out field work on forest conservation 
				which will enable forest managers, conservationists and 
				communities to understand how forests are responding to climate 
				change and how they can better manage them, he explains.<br>
				<br>
				Haroon was selected to participate in the workshop on the basis 
				of some criteria and commendable in-house environment-friendly 
				measures he has introduced at his department. “We had a big area 
				which was almost messy with lots of papers. We had a lot of 
				reports some which were not read at all. I reviewed all reports 
				and introduced some rationalisation. We stopped printing some 
				reports and started using double-side printers. By spending an 
				additional RO20, you can save a lot of paper. Besides, we 
				discourage use of hardcopies for internal communication and make 
				use of internal servers instead to send them across. We also 
				accord more importance to planting trees and encourage all our 
				employees to voluntarily adher to the disciplines of switching 
				off lights and printers after use,” he says.<br>
				<br>
				The seven-day stay in Oxfordshire, where HSBC’s Europe regional 
				centre is situated, included workshops, presentations on various 
				topics related to climate change and visits to the forest. “We 
				spent four days in the forests watching trees. We are told how 
				old they were and how changes in climate were going to affect 
				them. The Earthwatch is doing research on the entire fauna and 
				flora in the forest including the fallen leaves, which, they 
				said, still produce oxygen. That is why millipedes and 
				centipedes still feed on them. They warn climate change will 
				greatly affect the entire organics system,” Haroon says.<br>
				<br>
				Haroon makes it clear that HSBC is committed to the initiative 
				taken by the government for environmental and energy 
				conservation. “We are part of Oman Green team, dedicated to 
				extending the bank’s environmental awareness and conservation 
				efforts to suppliers, employees and wider stake holders. The 
				government is doing a lot more in this regard. The announcement 
				of His Majesty Sultan Qaboos bin Said to cultivate one million 
				palm saplings across the country and the recent Royal orders to 
				plant 100,000 coconut trees in the Governorate of Dhofar will 
				undoubtedly contribute to increasing the green cover of the 
				country. In the workshop, they have little knowledge about the 
				initiatives we have taken for natural conservation.<br>
				<br>
				Haroon thinks that in his capacity as financial controller he 
				can recommend some environment-friendly policies. “For instance, 
				I think there are no recycling units in Oman. If some companies 
				will come forward to set up a recycling unit, HSBC would have a 
				policy to support it” he says.<br>
				In 2005, HSBC was the first major bank – and FTSE 100 Company – 
				to become carbon neutral.<br>
				<br>
				He stresses the necessity of utilising renewable energy as an 
				alternative energy source. “In gulf countries we are not making 
				use of renewable energy. Since cars are more affordable and oil 
				is cheap, we exude more carbon dioxide. Renewable sources of 
				energy are very costly and less efficient. Until and unless the 
				cost-barrier is abolished, we cannot spend more on the renewable 
				energy.<br>
				<br>
				Haroon also informs that HSBC is seriously considering 
				unification sooner or later. HSBC Oman will soon introduce an 
				integrated machine which will do the functions of photocopier 
				and printer. The machine will not start printing unless one goes 
				there and puts the ID card in. It can save unnecessary 
				consumption of papers due to carelessness.<br>
