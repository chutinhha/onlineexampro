				</b><font face="Verdana" size="1">Forty years is a short span of 
				time in the life of a nation, but in the case of Oman it has 
				been four magical decades. The development and prosperity seen 
				by the Sultanate since the blessed Renaissance has been nothing 
				short of a miracle. The modern history of our nation has been 
				shaped by the wisdom, vision, courage and foresight of His 
				Majesty Sultan Qaboos bin Said. This issue of OER is a tribute 
				to this illustrious personality. <br>
				<br>
				A few things stand out in this month’s cover story. One, is how 
				Oman remained oblivious to the march of human civilisation until 
				July 23, 1970. An isolated state it had no relations with other 
				Arab or Islamic countries and the same isolation pervaded all 
				aspects of Omanis’ lives. Curfews were imposed, anyone found 
				outside the city walls after the retort of the cannons would be 
				shot unless he carried a lantern. Radios were banned, healthcare 
				was virtually non-existent. In 1970, there was only one 
				missionary hospital in Muttrah and a handful of admission units 
				in Muscat. Education too cut a sorry figure with three schools 
				existing across the whole State. On an average one school was 
				built every 19 years. <br>
				<br>
				Second, was the way Omanis responded to the clarion call of His 
				Majesty in 1970 to join in the efforts to rebuild their nation. 
				A number of Omanis came back from various countries to 
				contribute their mite to the process. His Majesty’s statements 
				like we will educate our children, “if only under the shade of a 
				tree,” galvanis ed the nation into promoting literacy. Within 
				the first five months of his rule, 16 primary schools were 
				established to educate over 9,500 pupils, a 662 per cent 
				increase in the number of children receiving education. 
				Additionally, girls were able to receive free government 
				schooling for the first time. Educational establishments spread 
				dramatically across the Sultanate in the next five years. By 
				1975, there were 262 schools and institutions, comprising 213 
				primary schools, 45 preparatory schools, three secondary schools 
				and one teacher training institute.<br>
				<br>
				On the 40th National Day we would like to convey our heartfelt 
				gratitude to His Majesty for his visionary guidance and nation 
				building efforts. It is also time for the new generation to 
				renew their commitment towards continuing the great work done 
				since the blessed Renaissance.<br>
