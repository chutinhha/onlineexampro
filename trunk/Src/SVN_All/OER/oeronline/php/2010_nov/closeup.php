					<b><font face="Verdana" size="4" color="#C00000">Broadbasing 
					its choices<br>
&nbsp;</font></b></p>
                </td>
		</tr>
	<tr>
		        <td valign="top" width="100%" style="border-style: solid; border-width: 1px; padding: 0"> 
				<p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="2"><b>The new GCC roadmap of 
				cooperating with new regional blocs will enhance its economic 
				choices and rectify the traditional trade imbalance that these 
				countries have suffered from<br>
				</b></font><font face="Verdana" size="1"><br>
				Bahrain is spearheading efforts to forge closer ties between 
				Gulf Cooperation Council (GCC) countries and other regional 
				blocs, and for noble reasons. This is happening ahead of a 
				Bahraini candidate, public security chief Abdul Latif bin Rashid 
				Al-Zayani, assuming the rotating chair of secretary-general of 
				GCC. Al-Zayani would take over his job from Qatari Abdulrahman 
				Al-Atiyyah after winning endorsement from the 31st summit 
				scheduled to take place in Abu Dhabi towards end-2010.<br>
				<br>
				Putting the new vision to work, GCC foreign ministers met in 
				Manama in late June to discuss a Bahraini roadmap for the group, 
				which amongst others call for economic-cooperation with regional 
				economic blocs and a joint action over human rights. 
				Undoubtedly, it makes every sense for GCC officials to explore 
				fresh ways every now and then in order to strengthen the group’s 
				position on a global level in an ever-changing socio-economic 
				and socio-political landscape. <br>
				<br>
				</font><font color="#000080" size="2" face="Verdana"><b>Reaching 
				out</b></font><font face="Verdana" size="1"><br>
				<br>
				Bahrain first offered the roadmap during GCC summit in Kuwait in 
				late 2009, and ostensibly used the first half of 2010 to explore 
				some of its key proposals. For example, Bahrain Foreign Minister 
				Sheikh Khalid bin Ahmed bin Mohammed Al-Khalifa has spent a 
				considerable amount of time talking to officials from the 
				Association of South-East Nations (Asean). Not surprisingly, 
				strengthening economic ties of six-nation GCC with 10-nation 
				Asean is a key component of Bahrain’s roadmap. Certainly, 
				reaching out to Asean neatly fits the GCC’s efforts of spiraling 
				presence in south-east Asia at large. To be sure, GCC’s first 
				ever deal with any nation was signed in late 2008 with Asean-member 
				Singapore. <br>
				Reinforcing economic cooperation with Asean falls in line with 
				newly-founded drive in GCC for reaching out to diverse economic 
				blocs. Earlier this year, GCC signed a memorandum of 
				understanding on commercial and economic cooperation with 
				19-member Common Market for Eastern and Southern Africa (Comesa), 
				the first with an African grouping. In 2009 GCC signed a free 
				trade agreement (FTA) with the European Free Trade Association 
				(EFTA), the first such deal with a regional bloc. EFTA comprises 
				Switzerland, Norway, Iceland and Liechtenstein. <br>
				<br>
				</font><font color="#000080" size="2" face="Verdana"><b>Reviving 
				talks with EU </b></font><font face="Verdana" size="1"><br>
				<br>
				Another piece of good news relates to resumption in June 2010 of 
				an annual economic dialogue between GCC and European Union. The 
				last time the two sides had such a dialogue was in 2003. Meeting 
				in Brussels, representatives from EU and GCC discussed three 
				major issues, the first being consequences of the global 
				financial crisis and lessons from the experiences of both sides 
				in addressing the debacle. The second topic concerned comparing 
				notes on implementation of common market projects in both 
				entities. In retrospect, GCC commenced implementation of Gulf 
				Common Market (GCM) project in 2008, in turn designed to allow 
				free flow of factors of production in member countries. Yet, the 
				third item on the agenda related to EU’s handling of Greece’s 
				debt problem. <br>
				<br>
				It is probably fair to assume that the EU side felt obliged to 
				engage GCC in the aftermath of Greece’s problem, which amongst 
				others brought about pressures on the value of Euro, in turn a 
				currency for some 16 EU members. Possibly, EU needs access to 
				GCC markets and investments nowadays in order to help addressing 
				its own problems resulting from Greece’s debt crisis. All said 
				and done, the two sides agreed on the need to continue the 
				dialogue with the eventual aim of signing an FTA between the two 
				blocs.<br>
				<br>
				Looking back, the GCC’s General Secretariat suspended talks with 
				the EU in late 2008 in order to signal its displeasure with 
				derailed negotiations. The decision was supposedly designed to 
				apply pressures on the EU in order to modify its conditions 
				prior to singing a deal with the GCC. <br>
				<br>
				</font><font color="#000080" size="2" face="Verdana"><b>
				Addressing EU demands</b></font><font face="Verdana" size="1"><br>
				<br>
				Yet, EU countries insist on addressing issues related to human 
				rights and democracy as precondition for signing a trade accord 
				with GCC states. In addition they also insist on the 
				proliferation of democratic teachings in the GCC.<br>
				<br>
				On their part, GCC states demand removal of customs charges on 
				aluminum and petrochemicals products. The EU imposes a six per 
				cent customs duty on imports of aluminum from the GCC. However, 
				the EU remains weary of governmental support such as 
				under-priced gas to GCC producers, a matter that grants them 
				unfair advantage against their European counterparts notably 
				countries joining since 2004. Some 12 countries joined the EU in 
				a span of five years including Romania, Poland, Hungary and the 
				Czech Republic.<br>
				<br>
				Needless to say, it is to GCC’s advantage to conclude a deal 
				with the EU, in turn the largest trading partner for regional 
				countries. The EU has a comfortable surplus in its trade account 
				with GCC countries. At the same, it is increasingly emerging as 
				a primary receiver of foreign direct investments (FDI) from GCC 
				states. Official EU statistical sources put the amount of FDI 
				from GCC in 27-member bloc at 63.2 billion Euros in 2008. 
				Conversely, EU countries pumped merely 18.9 billion Euros to GCC 
				economies in the same period. Yet, GCC’s appetite for 
				investments in EU members remains as steady as ever, a case in 
				point being the decision by a sovereign Qatari fund to purchase 
				Harrods for some $2.2bn. <br>
				<br>
				Clearly, lack of FTA between the sides is to EU’s advantage by 
				virtue of enjoying surpluses in trade and investments. As such, 
				GCC countries need to have unrestricted access to EU countries, 
				in turn the largest economic bloc in the world.<br>
				<br>
				Undoubtedly, time is ripe for GCC to reach out to different 
				economic blocs and countries. GCC countries have started free 
				trade talks with Japan, China, South Korea, and Pakistan plus 
				Australia and New Zealand. Also, Turkey and Iran are pushing for 
				separate trade deals with the GCC as a group. In the age of 
				globalisation, economic cooperation with others is the only way 
				forward, with GCC being no exception. <br>
