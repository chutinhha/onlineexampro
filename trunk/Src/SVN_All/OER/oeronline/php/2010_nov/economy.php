                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Transport 
					tune-up</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<br>
					<font face="Verdana" size="2">
					<b>
					In 
					a globalised economy which relies heavily on shipping, 
					Oman’s infrastructural upgrades ensure that it will continue 
					to be a desirable shipment and transshipment hub well into 
					the future<br>
					</b></font><b><font face="Verdana" size="1"><br>
					</font>
					</b><font face="Verdana" size="1"><br>
					Oman’s position at the heart of major trade routes between 
					Asia, Africa, and the Middle East has made it a shipping hub 
					for millennia. But there are changes afoot in this ancient 
					sector – changes which have the potential to fundamentally 
					alter the Sultanate’s position in world markets and the 
					underlying economy of the Sultanate itself.<br>
					<br>
					Broadly speaking, Oman’s government has made a significant 
					commitment to developing the country’s transportation 
					networks, providing a sophisticated grid for the movement of 
					goods throughout the second largest country in the GCC. In 
					keeping with the Sultanate’s desire to become a fully 
					diversified and sustainable economy by 2020, the air, rail, 
					road and – most extensively – port upgrades should make the 
					Sultanate ever more successful at handling the transport 
					demands of its growing population, as well as add much more 
					to its development plans.<br>
					<br>
					The projects, most of which were initiated as part of the 
					‘Vision 2020’ in the course of the seventh five year 
					development plan, are projected to be complete by 2017. The 
					latest batch was announced in July, when 15 agreements were 
					signed by the Ministry of Transport and Communications to 
					fund projects worth a total of RO136.9mn.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>AT 
					THE Crossroads</b></font><font face="Verdana" size="1"><br>
					<br>
					But other countries in the region have undertaken 
					infrastructural upgrades, I hear you say. Qatar and the UAE 
					have state-of-the-art airports, Saudi Arabia has some good 
					train networks and Dubai even has a metro. What makes Oman’s 
					new transport network so revolutionary? The reason is as 
					simple as a grammar school class: namely, geography.<br>
					<br>
					Oman, as previously mentioned, sits at the crossroads of 
					three continents. Even more importantly, in a globalised 
					economy which relies heavily on shipping, it sits at the 
					junction of three strategically significant bodies of water: 
					the Arabian Gulf, the Gulf of Oman, and the Arabian Sea. And 
					most important of all, it borders the Straits of Hormuz.<br>
					<br>
					This funnel, through which 40 per cent of the world’s 
					seaborne oil passes every year, is, without a doubt, crucial 
					to global energy supplies. It is also vital to the 
					fast-growing economies of the Gulf countries as the artery 
					through which the bulk of their imports and exports must 
					pass. If, for any reason, the Straits were to become 
					unnavigable, overland travel through the Sultanate would 
					become the natural, if not only, choice for transporting 
					goods in and out of this region.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Sensible alternative</b></font><font face="Verdana" size="1"><br>
					<br>
					The mysterious damage to the Japanese oil tanker M Star on 
					July 28 heightened awareness of the vulnerability of ships 
					passing through the Straits. A week after the incident, an 
					al-Qaeda affiliate known as the Abdullah Azzam Brigades 
					claimed the explosion, which left a large dent on the hull 
					of the tanker was the result of an attack by one of their 
					suicide bombers.<br>
					<br>
					While the damage was, in the end, not life-threatening, the 
					news sent shock waves through the shipping industry. It is 
					important not to blow this event out of proportion, but the 
					fact remains that the Straits are a mere 21 miles across and 
					there is a chance, however small, that this waterway could 
					become impassable for a non-trivial period of time.<br>
					<br>
					If that were to happen, goods would have three main avenues 
					in and out of the GCC. Goods coming from or destined for 
					Central Asia, the Caucasus, and Turkey could make their way 
					through Kuwait and on through Iraq. Goods sourced or bound 
					for Europe could go out from Saudi Arabia’s red sea ports 
					and onto the Mediterranean (though the amount of time this 
					would take would be unsuitable for food and other 
					time-dependent items).<br>
					<br>
					But for trade with Africa, the Americas, and, crucially, 
					Asia, the only sensible option would be to pass through 
					Oman.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>Port 
					upgrades</b></font><font face="Verdana" size="1"><br>
					<br>
					Sohar port, located on the northeast shore of the Sultanate 
					on the Gulf of Oman, is a natural choice for much of this 
					trade, and current improvements will make it all the more 
					fit. The existing berths at the port are, on average, 18.5m 
					deep. In addition, Sohar is the first port set to be 
					connected to the upcoming Oman National Railway and, 
					eventually, the GCC-wide train network.<br>
					It is also undergoing a significant capacity expansion to 
					keep up with projected demand. Nearly 85 per cent of the 
					land allocated to the company that runs the port – a 50-50 
					joint venture between the government of Oman and the Dutch 
					Port of Rotterdam – in its original concession is currently 
					leased, which led the company to seek additional land. 
					Granted earlier this year, the expanded concession will 
					start development with a 500 hectare Phase I.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>Duqm 
					develoPment</b></font><font face="Verdana" size="1"><br>
					<br>
					An additional boost should come from the massive development 
					project at Al Duqm, halfway down the Omani coast between 
					Muscat and Salalah.<br>
					This project, which aims to create a fully-fledged 
					industrial city and major port, is well on its way to coming 
					to fruition, with construction of the region’s largest dry 
					docking facilities due to be complete by 2010 and the 
					feasibility studies under way for a massive pipeline to 
					bring gas and oil from Saudi Arabia. Al Duqm’s location, as 
					the closest port to the vast natural gas fields of Saudi 
					Arabia’s Eastern Region, hold tremendous potential both as 
					an industrial centre – powered by economical natural gas – 
					and a &#8232;shipping hub.<br>
					<br>
					Of course, it is hoped that Oman will never have to become 
					the trading artery that bridges the GCC and Asia, Africa, 
					and the Americas. Regardless, Oman’s infrastructural 
					upgrades ensure that it will continue to be a desirable 
					shipment and transshipment hub well into the future.<br>
