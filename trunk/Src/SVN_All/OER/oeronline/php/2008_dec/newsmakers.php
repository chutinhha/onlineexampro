			<b><font face="Verdana" size="4">HONOURING THE POSITION</font></b><font face="Verdana" size="1"><br>
			<b>Majeed Al Asfoor’s election as the Chairman of the Oman Tennis 
			Association is a befitting tribute to his talent and association 
			with the game</b><br>
			<br>
			<img border="0" src="newsm12.jpg" width="180" height="214" align="right" hspace="8" vspace="6">Majeed Al Asfoor, executive manager, marketing, public relations and 
			information, Oman Arab Bank has been elected as the chairman of the 
			Oman Tennis Association (OTA). Al Asfoor takes charge for a 
			four-year term till 2012. He was chosen amongst the top ranking 
			Omani tennis players since 1974 until his retirement from the 
			national team. He won the first official tournament organised in 
			1986 by the OTA. In 1977 he became the Omani player to take part in 
			the First International Tennis Tournament organised in the Sultanate 
			of Oman at Gulf Hotel (Crowne Plaza), along with a number of world 
			ranking players. He won the third prize at the Gulf Tennis 
			Tournament in 1979 in Bahrain.<br>
			<br>
			In his speech on the occasion Al Asfoor expressed his thanks and 
			appreciation to the heads and representatives of the clubs for 
			electing the new board and the ministry of sports affairs for 
			supervising the conduct of the elections. Says Al Asfoor, “We will 
			continue the efforts of our predecessors and promise to work towards 
			the development of tennis in the Sultanate.” Al Asfoor was one of 
			the founder members of the OTA in 1986. He worked in all the 
			committees of the association at different intervals and was its 
			Vice president before he resigned in 1996.<br>
			<br>
			Al Asfoor represented the OTA at the International Tennis Federation 
			(ITF), the Asian Association, the Arabian Association and the 
			Organising Committee for Tennis at the GCC for 10 years, from 1986 
			to 1996. He has also worked as the general secretary, Organisation 
			Committee for Tennis, in the GCC. Al Afsoor’s election is surely a 
			homecoming of sorts for the tennis veteran. </font></p>
