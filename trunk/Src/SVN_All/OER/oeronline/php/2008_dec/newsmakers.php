			GEARING UP</font></b><font face="Verdana" size="1"><br>
			<b>HE Abdul Rahman al Attiyah, GCC Secretary-General talks of the 
			agenda of the forthcoming GCC summit<br>
			</b><br>
			<img border="0" src="newsm1.jpg" align="left" hspace="8" vspace="6">HE 
			Abdul Rahman Al Attiyah, GCC Secretary-General was in Muscat 
			recently to address the opening session of a seminar on Gulf Common 
			Market and Food Security, at the Oman Chamber of Commerce and 
			Industry. “The Sultanate is a leader and a pioneering country in 
			meeting the requirements of 10 paths of the Gulf common market, an 
			indication of the good implementation of decisions pertaining to GCC 
			citizenship,” said HE Attiyah. He added that food security will be 
			an important issue at the forthcoming GCC summit to be held in 
			Muscat in December, 2008.<br>
			<br>
			Referring to the food security seminar held in Riyadh in November 
			2008 H E Attiyah said it contributed to formulating a joint policy 
			on food security. The recommendations of Riyadh and Muscat seminars 
			will be presented to the Muscat Summit, he added. The 
			recommendations focused on a few main themes – local production of 
			foodstuff, GCC food imports and foreign investments. The primary aim 
			of these initiatives is to formulate a contingency plan to face any 
			lack of food supplies in GCC countries. He also talked about the 
			progress made by the GCC common market. HE Attiyah noted that the 
			announcement of the Gulf common market has come as an additional 
			incentive for the GCC governments and citizens to focus on major 
			issues. “There is a need to eliminate any constraints that may have 
			limited the member states’ ability to benefit from the previous 
			resolutions. It is necessary to remind citizens and officials of the 
			rights and obligations secured by the Gulf common market,” he added.</font></p>
