				only imported steel bars were accepted in the Oman market, 
				especially in the interior region. By establishing a 
				distribution chain and co-coordinated marketing strategy, Hadid 
				Majan helped break the monopoly. The result is that today, the 
				steel produced in Oman is popular with the industry. The steel 
				manufactured by the group caters to the highest standards in 
				quality.<br>
