				<font color="#CC3300">Renaissance Services signs RO26mn syndicated finance 
					facility</font><br>
					</font></b>
				<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
				<img border="0" src="bus1.jpg" width="250" height="168" align="left" hspace="8" vspace="6">Renaissance Services concluded a RO26mn syndicated finance 
					facility with Bank Dhofar, Bank Sohar and Oman Arab Bank. 
					The syndicated credit facility is arranged by Oman Arab Bank 
					and it is equally shared by the three banks. These funds 
					will be invested by Renaissance in its expansion projects in 
					Oman.<br>
					<br>
					Recently Renaissance has been awarded contracts by Petroleum 
					Development of Oman (PDO) to build, own and operate PAC 
					facilities in the Sultanate’s interior oil fields at Marmul 
					and Bahja. Renaissance has a recognised track record in 
					delivering strong returns from long-term projects of this 
					kind in Oman and abroad.<br>
					<br>
					Local banks have recognised this strength in the Renaissance 
					business model and have structured the terms of the facility 
					accordingly. Considering the current global shortage of 
					liquidity for long-term project funding, Renaissance has 
					secured the most reasonable terms and competitive pricing 
					for this 10-year facility.<br>
					<br>
					In the last two years Renaissance has invested more than 
					$400mn in capital projects and has secured appropriate long 
					term funding sources to support this. The recent third 
					quarter interim statement issued by Renaissance highlighted 
					the company’s efficient capital structure and the strength 
					of its business model with secure long-term projects in the 
					oil &amp; gas industry that have secured earnings and operating 
					cash flows for the group for future years. This new facility 
					serves to underwrite the current growth plans of the group.<br>
					<br>
					The financial closure of this credit facility in such a 
					challenging global financial market emphasises the robust 
					health of the banking system in Oman and is a strong vote of 
					confidence in Renaissance and its long-term projects.</font></p>
