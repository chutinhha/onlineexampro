					and beverages and automotive to manage quality processes, 
					regulatory and industry-mandated compliance and corporate 
					governance initiatives. MetricStream is headquartered in 
					Palo Alto, California (USA) with offices in Atlanta, 
					Georgia, and Bangalore, India.<br>
					<br>
					The Central Bank of Oman will use the MetricStream solution 
					to manage the complete audit lifecycle including risk-based 
					audit planning and scheduling, development of standard audit 
					plans and checklists, field data collection, development of 
					audit reports and recommendations, review of audit 
					recommendations by auditees and management and 
