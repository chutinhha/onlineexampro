			<font size="4" face="Verdana, Arial, Helvetica, sans-serif">Al Qurum 
			Properties unveils ‘Qaryat Qurum’</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<br>
			<img border="0" src="news3.jpg" width="250" height="168" align="left" hspace="8" vspace="6">Al Qurum Properties (AQPL), a joint venture between ETA Star and OHI 
			group, unveiled ‘Qaryat Qurum’ – a premium luxury township in the 
			heart of Muscat. Qaryat Qurum’s residential and commercial 
			properties are spread over an area of 128,934 sqmts and a total 
			development area of over 500,000 sq mts. The development has been 
			broken down into 2 major categories – The Main Centre and the 
			Hilltop Residences. The Main Center comprises of a retail area 
			(approx 52,000 sq mt), Commercial Towers (approx 95,000 sq mt), 
			Business Hotels (Approx 150 rooms) and Service Apartments (approx 
			8000 sq mt). The Hill Top Residences comprises of 13 towers of 1, 2, 
			3, &amp; 4 BHK Duplex apartments. The exquisite residences have been 
			carefully designed to offer an extensive landscape, a plethora of 
			modern amenities and contemporary architecture with a distinct 
			Arabic flavor. Only Omanis and GCC nationals are entitled to own 
			property in Qaryat Qurum.<br>
			<br>
			Qaryat Qurum residences will have several unique features like a 
			smart home system, a private garden, private terraces, a common 
			swimming pool as well as one in every building, club houses and 
			state of the art health centers. A gated community, Qaryat Qurum 
			epitomises the very best of the ‘Qurum culture’ – where tradition 
			blends with modernity in a distinctive fashion.</font></p>
