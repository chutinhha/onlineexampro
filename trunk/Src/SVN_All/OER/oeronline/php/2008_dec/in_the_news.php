                <font size="4" face="Verdana, Arial, Helvetica, sans-serif">NTI 
			announces BiZPro Awards 2009</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<br>
			<img border="0" src="news1.jpg" align="left">National Training Institute (NTI) announced the commencement of 
			enrolments for 2009 NTI BizPro Awards which have now come to be 
			coveted as hallmarks of Omani excellence for business and executive 
			competence. The awards are instituted by NTI to recognise and 
			celebrate the accomplishments of Omanis who have contributed 
			significantly to the economy of Oman. Feedback, from the Omani 
			executive world specifically, has indicated that there is much 
			appreciation of NTI’s intent, and the BizPro Awards event is one 
			that people want to get associated with.<br>
			<br>
			In 2009, one Business Leader award will be conferred on an Omani 
			businesswoman. There is a separate award in the men’s category. In 
			the Young Achiever segment, three Omani awardees will be nominated. 
			All the prizes are sponsored by Renaissance Services, which has 
			always subscribed to ethical corporate governance, excellence in 
			business, and encouraged empowerment and the peaking of individual 
			competence.<br>
			<br>
			The voluntary participation of the renowned KPMG organisation in 
			Oman as evaluators of candidates is a special feature of the 2009 
			BizPro Awards. KMPG is known for its professional status in audit &amp; 
			business consultancy, and its involvement will assure candidates of 
			even greater transparency in the evaluation process.<br>
			<br>
			National Training Institute (NTI), the leading education and 
			training institution in Oman said the evaluation process has 
			started, wherein candidates will be chosen for the five best Omani 
			Business Leaders &amp; Young Achievers. In 2008, good corporate level 
			participation was seen. 2009 promises even more enthusiasm, as the 
			NTI BizPro Awards have drawn much appreciation from the entire 
			business community in the Sultanate for pioneering 
			conceptualisation.<br>
			<br>
			Over the years the NTI BizPro awards have been able to inspire 
			industry people treading the way towards establishing themselves as 
			business pioneers. NTI’s BizPro Awards has paved the way to 
			recognise talented Omanis who have the innate ability to steer the 
			business world. The Awards also aim to develop and encourage 
			leadership and life skills in young Omanis, develop a sense of pride 
			in being an Omani, and provide role models and mentors for youth by 
			highlighting their achievements and the pursuit of excellence. <br>
			<br>
			NTI BizPro Awards endeavour to become a showcase event, with 
			increased levels of participation from highly competent candidates 
			from the corporate world in Oman. The selection criteria are 
			methodical, and the 2009 Awards promise a leap forward in qualifying 
			standards. The final winners will be solely recommended by the KPMG 
			Team. The Business Leader Award carries a scholarship of RO5,000, 
			while each of the Young Achievers will receive a scholarship of 
			RO2,500.<br>
			<br>
			NTI – a part of the Education and Training Group of Renaissance 
			Services, is synonymous with quality training in Oman. Today NTI, 
			provides high quality training solutions in HSE, technical, IT 
			business skills and financial studies, and specialises in developing 
			indigenous workforce. The initiative to identify unique and 
			outstanding talent hidden in Oman, by NTI BizPro, has set a stage to 
			diversify professional excellence. NTI is once again gearing up to 
			set a similar stage for brilliance in pro-fessionalism.
