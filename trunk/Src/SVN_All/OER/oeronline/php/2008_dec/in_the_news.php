			<font size="4" face="Verdana, Arial, Helvetica, sans-serif">Al 
			Madina A’Zarqa-Anantara Hotels pact<br>
			</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
			<br>
			<img border="0" src="news2.jpg" width="350" height="188" align="right" hspace="8" vspace="6">Blue City Company 1 (BCC1) made an agreement with internationally 
			recognised luxury hotel group, Anantara Hotels &amp; Resorts, for a new 
			beachfront hotel development in Phase One of Al Madina A’Zarqa.<br>
			<br>
			Anantara Hotels &amp; Resorts, part of the Minor Hotel Group, one of 
			Asia’s leading hotel owners and operators is internationally 
			recognised as leaders in luxury hospitality. This agreement with 
			Anantara signals the launch of the first-ever hotel alliance for Al 
			Madina A’Zarqa, as well as the debut of Anantara in Oman.<br>
			<br>
			Roger Kacou, vice president of Hotels and Leisure Development, BCC1 
			said: “I am delighted to be welcoming Anantara to Al Madina A’Zarqa. 
			The alliance combines the strength of Anantara’s experience in 
			designing, developing and managing luxury hotel developments with Al 
			Madina A’Zarqa’s commitment to building a city of the future and 
			placing Oman on the map as a premier tourist and business 
			destination in the Middle East.”<br>
			<br>
			The luxury destination will offer residents and visitors alike 
			outstanding facilities, exceptional service and a broad range of 
			rooms delivered in an environment of unmatched elegance and refined 
			luxury. It will also feature a 5-star spa that will offer its guests 
			an unmatched range of services and treatments. Supported by 
			world-class dining options, the beach front resort will provide a 
			unique blend of ambience and exclusivity. Dilip Rajakarier, chief 
			operating officer of Minor International PCL, the company heading 
			Anantara Hotels &amp; Resorts said: “We are excited to have signed this 
			MOU with such a prestigious development as Al Madina A’Zarqa as a 
			first step towards our entry in Oman. We believe Oman, with its rich 
			culture and heritage will be an ideal location for our luxury 
			Anantara brand and will complement our developments throughout the 
			GCC region. We are confident that this partnership will create one 
			of the most talked-about destinations in the world.”<br>
			<br>
			Born in Thailand in 2001, the Anantara Resorts and Spas is a 
			collection of five-star luxury and boutique resorts and spas, 
			providing an immersion in culture, history, and unique natural 
			settings. The Anantara chain of hotels has been named one of the 
			‘Top 10 overseas spa hotels’ and currently includes four award 
			winning resorts and spas located in the Maldives. <br>
			<br>
			“Oman is well on the way to become a leading business and tourist 
			destination, with vast potential in terms of tourism growth which 
			will complement Oman’s 2020 vision. With the added attraction of 
			Oman’s rich cultural heritage and breathtaking scenery, we 
			anticipate a great deal of interest in this luxurious Anantara hotel 
			in Al Madina A’Zarqa” concluded Kacou.
