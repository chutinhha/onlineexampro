                    </font><b><font face="Verdana" size="4">Bearish Mood 
				Persists</font></b><font face="Verdana" size="1"><br>
				<b>As the global financial uncertainty continues investors are 
				staying away from regional stock markets preferring to hold onto 
				their cash</b><br>
				<br>
				MSM30 index slumped almost 1,500 points to close at 6,222 points 
				for the period from October 16 to November 15, 2008 taking cues 
				from the global markets. Worldover, investors feared the 
				repercussions of the financial crisis on various sectors. In the 
				regional markets, weak global demand expectations led to steep 
				fall in crude oil prices instigating worries over governments 
				spending.<br>
				<br>
				<img border="0" src="mktv1.jpg" width="328" height="285" align="left" hspace="8" vspace="6">In the local market, investors preferred to hold onto their cash 
				this translated into a low average daily turnover of RO10.8mn. 
				Amongst the sectoral indices, industry and banking indices 
				tumbled the most by approximately 24 per cent each, while the 
				service index was down by 19 per cent. <br>
				<br>
				<b>Corporate events<br>
				</b>A number of companies have lined up to buy-back their shares 
				giving the market an indication that their stocks are trading at 
				attractive levels. In corporate events, Oman Hotels &amp; Tourism 
				and Al Sharqiya Hotels and Tourism Company announced plans to 
				merge the two companies to achieve operational and economic 
				efficiencies. Oman Flour Mill signed a memorandum of association 
				with International Foodstuffs Company (IFFCO) to set up a 
				poultry project near the Oman UAE border to cater to the 
				regional market. The new company will have a layer farm with 
				1.2mn layers and will gear up the production to 250mn eggs per 
				year. In other developments, Oman Investment and Finance Company 
				announced its plans to buy back shares upto 10 per cent of the 
				value of its paid up capital. Dhofar Insurance also announced 
				its intent to repurchase shares to the tune of 10 per cent of 
				the company’s’ shares from the paid up capital of RO20mn.<br>
				<br>
				<b>Mood on GCC markets <br>
				</b>The month ended November 15 was another disappointing one 
				for the GCC markets and the MSCI GCC index plummeted 28.17 per 
				cent to 378.42 points. All the bourses registered a double digit 
				decline with the Dubai Financial Market (DFM) topping the list 
				with a fall of 38.56 per cent. All the major indices closed on a 
				negative note with real estate and investment indices taking the 
				lead, declining 47.74 per cent and 37.88 per cent respectively. 
				The DFM is the biggest YTD decliner and has lost 64.49 per cent. 
				The Doha Securities Market too ended on a negative note and 
				declined 27.32 per cent. The industrial and insurance indices 
				were among the top losers and with the indices declining 36.70 
				per cent and 32.02 per cent respectively. Despite the government 
				support of buying 20 per cent of the bank’s capital at the floor 
				price, the banking space declined by 29.47 per cent during the 
				month.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
				<img border="0" src="mktv2.jpg" width="489" height="355"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
				<br>
				The Tadawul All Share Index lost 25.99 per cent during the 
				month. Heavy selling was seen across all the major indices with 
				petrochemicals and industrial indices taking the worst hit. The 
				Kuwait Stock Exchange lost 25.84 per cent during the month. The 
				industrial sector was among the worst performers and lost 35.33 
				per cent. The banking sector which is a key sector in Kuwait was 
				seen in dilemma when Gulf Bank, the second largest bank in the 
				country, posted derivative losses of Kuwaiti Dinar 200mn. The 
				Abu Dhabi Securities Market (ADSM) and Bahrain Stock Exchange 
				too closed in red and the general indices declined 21.58 per 
				cent and 13.09 per cent respectively. While the real estate 
				sector was the worst performer on the ADSM losing 39.35 per 
				cent, the banks were among the top losers on the Bahraini 
				bourse. The global financial turmoil coupled with declining oil 
				prices were the prime reason for the lackluster performance 
				witnessed by the GCC bourses. The GCC governments have taken a 
				series of initiatives to support their respective economies via 
				deposit guarantees, share buy-backs, easing monetary policies 
				among others. Further, we expect momentum to pick up once global 
				uncertainty abates and recommend investor caution and value 
				driven stock picking. <br>
