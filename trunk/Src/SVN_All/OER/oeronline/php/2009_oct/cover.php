              <p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                <br>
                </font><b><font face="Verdana" size="4">Digital Delight</font></b><br>
                <font size="1" face="Verdana"><b>Leveraging information technology 
                to suit modern life is fast becoming a reality with the Sultanate 
                of Oman taking a lead in becoming IT-savvy. For a layperson, this 
                translates into getting practically all services at a click of 
                a mouse. Visvas Paul D Karra takes a closer look<br>
                <br>
                </b><img border="0" src="images/img22.jpg" align="right" hspace="8" vspace="5">“Information 
                technology and communications have now become the main elements 
                that move forward the development process in this third millennium; 
                therefore, we have accorded our attention to finding a national 
                strategy to develop the skills and abilities of citizens in this 
                domain with the aim of further developing e-government services. 
                We call upon all government institutions to speedily enhance their 
                performance, and to facilitate their services, by applying digital 
                technology in order to usher the Sultanate into the constantly 
                evolving spheres for applying knowledge.”– Excerpts of His Majesty 
                Sultan Qaboos Bin Said’s speech emphasising the need to implement 
                IT in every sphere of life<br>
                <br>
                Convenience is anything that is intended to save resources – in 
                terms of time, energy or frustration. For example, hypermarkets 
                like Lulu or Carrefour are enlarged versions of convenience stores 
                where everything is available under one roof and saves the consumer 
                from having to go to different stores for different needs. Digital 
                convenience, similarly translates into high-speed data access 
                and information (and service) availability at the tip of one’s 
                finger or the proverbial click of a mouse. As the ‘e’ word becomes 
                ubiquitous, the marriage of digital technology with a human being’s 
                daily life could well become the humdinger of the future.<br>
                <br>
                <b>At the forefront</b><br>
                Perhaps the biggest indicator of how well Oman has transformed 
                itself into a digital society is the recent Global IT Report 2008-09 
                featuring 134 countries published by the World Economic Forum. 
                The Sultanate is ranked 50th in the overall report with the government 
                readiness index of Oman scoring the 39th rank. Directed and energised 
                by His Majesty’s vision and directives for eOman, great strides 
                have been made in the field of information technology within the 
                Sultanate in terms of secure infrastructure, services, legislation, 
                online payments, frameworks, policies and awareness. As a result 
                of these continued efforts, Oman is positioned at 45 in government 
                usage, and this is based on the government’s success in ICT promotion, 
                availability of government services online, presence and efficient 
                use of ICT within the public sector and e-participation. In a 
                separate development, despite the global trends of economic downturn 
                and inflation, Oman is ranked 84th in the UN E-Government Readiness 
                Index 2008, a jump of 28 points from 2005. <br>
                <br>
                <img border="0" src="images/img20.jpg" align="left" hspace="8" vspace="5"> 
                The proof of the pudding lies in tasting it, they say and in this 
                regard, the Sultanate has done itself proud by allowing its citizens 
                to taste the benefits of the information, communication and technology. 
                The Royal Oman Police (ROP) has successfully implemented IT usage 
                for its public services and so has the Muscat Municipality. Similarly, 
                there are several government and public entities which have embraced 
                technology to make their services and transactions simpler for 
                the public. The keyword being digital convenience.<br>
                <br>
                And spearheading this digital movement is the Information Technology 
                Authority (ITA). Says Dr Salim al Ruzaiqi, CEO, ITA, “The global 
                rankings of Oman indicate the tremendous progress made by the 
                country in terms of moving towards a digital society. The projects 
                we handle are so different in nature, focus and purpose but together 
                they enable us to realise the main goal of ITA – eOman.”<b><br>
                <br>
                </b>ITA, which celebrated its third anniversary in June this year, 
                is responsible for the execution of all national e-government 
                projects to create the required national infrastructure systems 
                and build human resource capacity within the community. Within 
                a short span of three years, ITA has launched several flagship 
                projects such as the Government Convergent Network, ePayment Gateway, 
                Electronic Transactions Law, National Data Centre, Centre for 
                Information Security, Innovation and Support Centre, eOman Awareness 
                campaign, Government Training and Community Training programmes. 
                Its innovative projects such as the Online Donations Portal have 
                been receiving regional awards.<br>
                <br>
                <b>Customer convenience</b><br>
                So, what does this mean for the common man. While most of the 
                above mentioned ITA projects may sound geeky, the fact of the 
                matter is that all of them are inextricably linked to public services 
                and enable greater public convenience through the use of digital 
                services. Take for example the ePayment Gateway (ePG) which was 
                launched around a year ago. <br>
                <br>
                The ePG provides an operational component to e-governance infrastructure 
                and e-commerce facilities that allows secure online payments or 
                e-payments. The ePG suppor<img border="0" src="images/img19.jpg" align="right" hspace="8" vspace="5">ts 
                multiple banks and operates as a critical shared service within 
                the e-governance architecture along with Oman’s forthcoming government 
                eServices Portal. The state-of-the-art e-payment solution supports 
                the delivery of e-services of the public sector and assists the 
                private sector’s moves into e-commerce. Therefore, if you are 
                able to pay your utility bills from the convenience of your home 
                or office using your credit card then it is thanks to the ePG.<br>
                <br>
                Muhanna Moosa Baqer, manager, ePayments, ITA says, “Thousands 
                of transaction have taken place successfully over the ePG. The 
                presence of such a robust and secure ePG enables the public to 
                pay for government services electronically using major local and 
                international credit and debit cards, and in future, other electronic 
                payment instruments.” The ePG currently runs on the Internet and 
                will support various other electronic channels including the mobile 
                phone, IVR, Call Centre etc in future. Till date, more than 35,000 
                transactions worth more than a million rials have taken place 
                through the ePG. <br>
                <br>
                The ePG is the harbinger of the e-Purse which was introduced on 
                July 23 this year. The e-Purse could usher in a new era of cashless 
                transactions in the future. The e-Purse, introduced as part of 
                the national e-Governance initiative, will revolutionise all transactions 
                in future. To activate the e-Purse on existing ID cards, one has 
                to visit the office of the Directorate General of Civil Status 
                (DGCS) near Muscat. In future all new ID cards will come with 
                a built in e-Purse. In the second phase, customers will have the 
                option of loading the e-Purse with cash at other banks too. In 
                the initial stages, the e-Purse will be used only for government 
                and ROP services but once the pilot phase runs successfully, it 
                will cover a wide range of transactions.<br>
                <br>
                During the first phase, the e-Purse can be loaded with cash only 
                at BankMuscat branches, with the Point of Sale (PoS terminals) 
                network of the bank facilitating card-based transactions at ROP 
                directorates, ministries and government establishments. Dr Salim 
                says, “Being the first of its kind in the region, the e-Purse 
                conveys a strong Omani sentiment. Unlike other bank cards linked 
                to global payment systems for cash payment and settlement process, 
                all transactions done on e-Purse originate and end within Oman 
                thereby saving a lot of money for banks and service providers.”<br>
                <br>
                <b>Cashless era</b><br>
                Sulaiman al Harthy, group deputy general manager, consumer banking 
                group, BankMuscat, says, “With the introduction of e-Purse, the 
                cash payment landscape significantly changes in Oman. This also 
                marks a major shift in e-Governance initiatives launched by the 
                government.” <br>
                <br>
                ROP was one of the leading stake holders in the recently launched 
                e-Purse. This is truly a big leap forward in the Sultanate of 
                Oman and the first of its kind initiative in the re<img src="images/img17.jpg" width="350" height="289" hspace="8" vspace="5" border="0" align="left">gion. 
                Says Brigadier Baqer Mohamed al Saleh, director general of financial 
                affairs, Royal Oman Police HQrs, “It is proposed to extend this 
                usage to other organisations in a phased manner. This launch has 
                now enabled the public to use their national identity card or 
                resident card as a means of payment for all government agencies 
                and outlets having a POS system.”<br>
                <br>
                Talking about simplified government procedures for the public, 
                there is no organisation to beat the Royal Oman Police (ROP), 
                which started utilising the advancements in technology to serve 
                the people better. ROP was one of the foremost governmental agencies 
                as far back as 2002, to take the initiative in applying state-of-the-art 
                information technology systems for the benefit of citizens. One 
                of the earliest initiatives was to make the visa application and 
                information and traffic offence payments online through its award 
                winning website. Another monumental achievement was the implementation 
                of the National Registration System and e-Gate which enables citizens 
                and eligible residents seamless and instant completion of immigration 
                formalities by using their own identification cards. <br>
                <br>
                Says Brigadier Baqer, “ROP is the leading government organisation 
                to implement cashless transactions. We have started this by two 
                methods, firstly through bank issued cards and secondly through 
                the Internet payment gateway. Keeping customer convenience in 
                mind, a cashless system was introduced so that all payments to 
                ROP could be made through a secure electronic payment gateway 
                and there would be no need to carry cash to the ROP counters.”<br>
                <br>
                <b>One individual, one identity</b><br>
                Another initiative launched by the ROP and which has gained international 
                recognition is the Civil Status project launched in 2004 to maintain 
                the nation’s civil register using modern technologies to record 
                birth, death, marriages and divorces in the Sultanate and also 
                manage the electronic identity cards for all citizens and residents 
                of Oman.<br>
                <br>
                Over 12 civil status centres are electronically linked throughout 
                the country through a secure and reliable connectivity to process 
                data in real time with the required level o<img src="images/img14.jpg" width="350" height="365" hspace="8" vspace="5" border="0" align="right">f 
                accuracy and privacy. The ministry of education, ministry of health, 
                ministry of manpower, ministry of foreign affairs, ministry of 
                justice and the ministry of civil services are all stakeholders 
                in this comprehensive integrated system. In order to enhance their 
                systems the ROP conducted a Customer Satisfaction &amp; Service 
                Quality Survey in 2007.<br>
                <br>
                The state-of-the-art electronic chip-based national identity card 
                for citizens and resident card for expatriates is the result of 
                this project. Besides personal identification features, the ID 
                card offers a number of in-built options, facilitating a wide 
                range of services, which will be progressively activated. Presently, 
                the chip on the ID card carries information like blood group and 
                driving license details etc. But soon it is expected to become 
                the single most valuable card in an individual’s pocket. The e-Purse 
                is one such small but significant step. Soon all details about 
                the individual would be loaded on his/her ID card including his 
                medical records, electoral details, educational background etc.<br>
                <br>
                The provision of numerous applications equips the national ID 
                card to be used as a multi-purpose card. The e-Purse being one 
                example. Being an identity card, the e-Purse remains with citizens 
                and residents at all times. Whenever an e-Purse is used, the identity 
                of the user is verified and the government can track each transaction, 
                if necessary. Similarly, if a person meets with an accident and 
                he needs blood, then his ID card number can be keyed in and all 
                the relevant medical details can be obtained.<br>
                <br>
                <b>A uniform gateway</b><br>
                The relevance of the national ID card is realised in the context 
                of Oman’s official eGovernment Services Portal (www.oman.om). 
                Through the eGovernment services portal, both citizens and businesses 
                can access government information and services online. These servic<img border="0" src="images/img16.jpg" align="left" hspace="8" vspace="5">es 
                are being provided via the portal either by integrating it with 
                other government entities or by providing links to their websites. 
                The portal can be accessed anytime, anywhere through multiple 
                channels such as web and mobile devices. Navigation on the portal 
                site is simplified through a live event service delivery model 
                and searchable content.<br>
                <br>
                Says Yaqoob al Bulushi, chief of infrastructure and e-Service 
                Division, ITA, “The portal has been structured on a life event 
                (cradle-to-grave) model that provides services based on <br>
                the profile of the visitor and even for the disabled. This makes 
                navigation easy and users can use the service without a need to 
                know who is offering the service or even who is processing the 
                service. The portal will enable the public (both individuals and 
                corporates) to reach the concerned government entities at any 
                time, and from anywhere without having to physically visit their 
                offices.”<br>
                <br>
                The infrastructure and e-service division of ITA is responsible 
                for building up the national IT infrastructure, which will culminate 
                in the official eGovernment Services Portal. To be officially 
                launched before the end of 2009, the Portal will be the main gateway 
                to the government and its services. Any individual or organisation 
                with a national ID or a company registration number can log onto 
                the portal and avail of its services.<br>
                <br>
                Dhananjay Bopardikar, professional services manager, Gulf Business 
                Machines (Oman) adds, “The official eGovernment Services Portal 
                which was earlier known as Ubhar tracks ones journey through life. 
                Since every individual and entity will either be known through 
                their national ID or Company Registration (CR) number, therefore, 
                this portal will become a part and parcel of everyone’s life cycle 
                in the country.”<br>
                <br>
                GBM, the distributor for select IBM products and services in the 
                GCC excluding Saudi Arabia for 15 years, has been supplying IBM 
                hardware and software solutions for the eGovernment Services Portal. 
                It has also been closely linked with infrastructure development 
                being carried by the ITA. A number of government entities are 
                already integrated with the government portal, <br>
                www.Oman.om to provide their services electronically. Individuals 
                and companies can log on to the portal and customise pages to 
                their needs and requirements. In future, all government transactions 
                can be dealt through this single portal.<br>
                <br>
                <b>Ease of business</b><br>
                The One-Stop-Shop (OSS) is a highly useful service offered by 
                the ministry of commerce and industry (MoCI), which enables investors 
                and businessmen to<img src="images/img18.jpg" width="350" height="289" hspace="8" vspace="5" border="0" align="right"> 
                set up companies in Oman while minimising paperwork, saving costs 
                and time. All applications and governmental processes involved 
                in company registration and approval requests will be possible 
                online through the official eGovernment Services portal, making 
                it a single-entry access point for OSS services for appropriate 
                entities. The OSS has already improved services rendered to new 
                and existing businesses by minimising the number of visits needed 
                to complete the registration process. All payments for such services 
                can be made online through electronic channels like ePG and e-Purse.<br>
                <br>
                The OSS is a strategic tool which will contribute to MOCI’s objective 
                of creating a more transparent, easy and efficient delivery mechanism 
                for essential business enabling services. “GBM and IBM were key 
                suppliers for national e-Government projects across all the GCC 
                states. The OSS project is a showcase undertaking for GBM Oman 
                and we are proud to be associated with MOCI in its contribution 
                towards developing of the national economy. The easy-to-use portal 
                has facilitated MOCI’s aim to develop a swift, efficient and transparent 
                system,” says Olivier Mouries, general manager GBM Oman.<br>
                <br>
                <b>Civic services at hand</b><br>
                The Muscat Municipality which deals with nearly a million people 
                in the capital area has digitalised most of the services it provides 
                to the public. The country’s premier civic body has provided multiple 
                electronic channels for the public to get more information and 
                do transactions with the municipal office. “The most popular of 
                our electronic services is the SMS parking which saves a lot of 
                time and makes it easier for people to find parking. In addition 
                to this, we have various other services and people can visit our 
                website (www.mm.gov.om) to find out more. We have SMS services 
                (90888); IVR service (24753555); WAP service for mobile phone 
                (wap.mm.gov.om); e-payment; and the well known SMS (90091) car 
                parking facility,” informs Khalid Dawood al Zadjali, deputy director 
                general of information systems, Muscat Municipality.<br>
                <br>
                <img src="images/img15.jpg" width="350" height="334" hspace="8" vspace="5" border="0" align="left"> 
                The Muscat Municipality’s IVR (interactive voice response) service 
                is used for enquiring the status of the services applied for at 
                the Muscat Municipality telephonically; to know about the required 
                documents for municipal services; for suggestions and complaints. 
                The SMS service can be used to enquire about building permits, 
                rent contracts, licences, health and technical inspection, construction 
                permission, minor building permits by simply sending the service 
                registration code e.g. SR 2009035658 to 90888. Customers can check 
                their vehicle parking violations by sending VE followed by vehicle 
                registration number and code to 90888 e.g. VE 9626 B.<br>
                <br>
                The Muscat Municipality’s WAP site while giving you access to 
                the website using your mobile also enables people to check out 
                various activities and events (e.g. Muscat Festival) planned by 
                Muscat Municipality. Further, all Muscat Municipality users can 
                pay all the fees for municipal services using the electronic card 
                payment facility.<br>
                <br>
                The Muscat Municipality e-payment system is slightly different 
                and more innovative because of the integration of the revenue 
                system with the POS terminals (payment machines). As a result 
                while a payment is being made a Muscat Municipality staff does 
                not need to enter the payment amount in the POS terminal physically, 
                as the money gets sent directly from the POS terminal to the revenue 
                system. This is possible as the payment details have already been 
                sent to the POS terminal from the revenue system. <br>
                <br>
                <b>Education gets elevated</b><br>
                Prior to the Higher Education Admission Centre (HEAC) being established, 
                students passing out from schools had to run literally from pillar 
                to post to get admission in the college of their choice. If one 
                college rejected his/her application, then they would have to 
                run to another college. This was especially difficult for women 
                from interior regions as their parents would not allow multiple 
                trips to the city. All that has changed since 2006. <br>
                <br>
                Now all that a student, who has completed his general certificate 
                examination (or its equivalent) for admission to higher education 
                institutions (HEIs), has to do is enter the HEAC website, register 
                his application and wait for the results. The application is processed 
                electronically and an electronic system places students in their 
                preferred programmes based on their results. The new system is 
                highly accurate and ensures equality and transparency.<br>
                <br>
                The HEAC system also validates applicants for the 15,000 scholarships 
                that are available every year for 40,000 odd students who pass 
                out from school every year. The HEAC is located in Muscat and 
                operates with highly skilled professional staff. Dr Said bin Abdullah 
                al Adawi, director general of HEAC, ministry of higher education, 
                points out that the ministry wanted to ensure equality, fairness 
                and transparency in the admission process. “Since the time the 
                HEAC system was implemented in the academic year 2006-07, the 
                system has found social, psychological and economic success,” 
                says Dr Adawi.<br>
                <br>
                The robust IT backbone being implemented in the Sultanate has 
                the potential to change the way business is done, civic amenities 
                get accessed and government payments are made. A silent IT revolution 
                is underway and digital convenience may become a way of life sooner 
                <br>
                than anticipated.<b><br>
