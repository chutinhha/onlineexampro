		        <td valign="top"><div align="justify">&nbsp; <br>
                    <b><font face="Verdana" size="4">Collaborative Effort</font></b><font face="Verdana" size="1"><br>
                    <b>Jan Babiak, partner, global climate change and sustainability 
                    services leader, Ernst&amp;Young</b><img border="0" src="images/img48.jpg" align="right" hspace="8" vspace="6" width="150" height="168"><b> 
                    talks about the immediacy of environment protection to Mayank 
                    Singh. Excerpts</b><br>
                    <br>
                    <b>This is your first visit to Oman, what is the agenda of 
                    this trip? </b><br>
                    The purpose is twofold, one is to work with the local staff 
                    on climate change services and to share with them as to what 
                    we do across the globe and what our clients are finding beneficial 
                    so that they can serve their markets better. Second as a global 
                    leader, I help government’s and NGO’s to know as to how businesses 
                    can help efforts to lower carbon emission and increase sustainability. 
                    <br>
                    <br>
                    <b>What are the big challenges that the world faces in terms 
                    of climate change and sustainability?</b><br>
                    The first and the biggest challenge is the lack of understanding 
                    and the need for education so that people understand as to 
                    what needs to be done and who needs to do that. Then comes 
                    the other problem of inequities because you have the developing 
                    world which has a low carbon footprint and a desire to bring 
                    their population out of poverty and which will inevitably 
                    increase their carbon footprint, but globally we need to decrease 
                    this. So while the developed world does not want to compromise 
                    their quality of life, the developing world wants to improve 
                    their quality of life, and a combination of both is destroying 
                    the quality of life for all of us. The challenge is to find 
                    an equitable way of development while curtailing emissions 
                    and this is a very difficult challenge. <br>
                    <br>
                    <b>The issue of sustainable development has come to acquire 
                    political overtones, so how does one strike a balance between 
                    different standpoints? </b><br>
                    We need to move away from the view that this is an either 
                    or choice because in reality this includes everyone. As a 
                    <img border="0" src="images/img49.jpg" align="left" hspace="8" vspace="6">start 
                    we can begin with base line issues like what are we doing 
                    to make our buildings more efficient; what are we doing to 
                    make automotive travel less emission intensive. A lot of this 
                    can be done with existing technologies; measurement of carbon 
                    footprint so that we can start bringing it down. <br>
                    <br>
                    <b>There is a feeling that this is a problem that may occur 
                    hundreds of years from now and so it does not require immediate 
                    attention. How would you respond? </b><br>
                    Anyone who thinks that this is an issue that is even 100 years 
                    away is not reading the signs. According to an Intergovernmental 
                    Panel on Climate Change (IPCC) report, this is a problem that 
                    will impact us by 2020 and we are going to have displacement 
                    of people running into millions by 2050, if we do not do something.<br>
                    &nbsp;<br>
                    <b>Similarly, many feel that the magnitude of the issue is 
                    so large that individual efforts cannot make a difference 
                    and therefore it is best left to the government?</b> <br>
                    I agree that it is the government’s problem, but it is equally 
                    the responsibility of business and consumers. There can be 
                    a lovely virtuous circle between these three groups. Politicians 
                    are elected by people and they can be held accountable by 
                    consumers. Businesses have a critical role as a number of 
                    environment protection measures entails cost cutting, so they 
                    need to do this even as a part of their responsibility to 
                    shareholders. If businesses are not willing to make products 
                    that are environment friendly and consumers are ready to buy 
                    at a reasonable cost then the government needs to bring in 
                    legislation to force them to do so.<br>
