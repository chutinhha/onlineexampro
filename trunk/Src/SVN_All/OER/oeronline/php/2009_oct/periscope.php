                    <br>
                    </font><b><font face="Verdana" size="4">Can The Wall Street 
                    Bull Market Continue?</font></b><font face="Verdana" size="1"><br>
                    <b>An improved forecast for growth of global GDP and the rise 
                    </b><img border="0" src="images/img11.jpg" align="right" hspace="8" vspace="3" width="100" height="118"><b>in 
                    industrial production in the US send positive signals about 
                    the recovery from recession. Despite such sig</b><b>ns Investors 
                    still need to be cautious<br>
                    <br>
                    </b>Wall Street emerged from the post-Lehman credit crisis 
                    not with a whimper but a proverbial bang. The rally since 
                    March has been both swift and spectacular, with the SP500 
                    rising from its lows of 667 to above 1040 by mid-September. 
                    It is only natural for investors to question the sustainability 
                    and even logic of a 50 per cent rise in the world’s bellwether 
                    stock market index months after the worst credit shock to 
                    hit the international banking system since the Latin America 
                    sovereign loan defaults of the 1980s and the property crash 
                    after the 1990 Gulf War.<br>
                    <br>
                    The US economy has shrank for four consecutive quarters, corporate 
                    profits have plummeted at the fastest pace since the Great 
                    Depression, almost one out of 10 Americans is out of work 
                    with the jobless rate at a 26 year high. Further, the <br>
                    mightiest money centre bank on Wall Street (Citigroup) is 
                    a ward of Uncle Sam, a colossus that survived after a Washington 
                    financial bailout only because the Bernanke Fed judged it 
                    “too big to fail”.<br>
                    <br>
                    <b>Upbeat indicators</b><br>
                    It is not difficult to find a tangible uptick in macroeconomic 
                    indicators. The IMF improved its forecast of global GDP growth 
                    to 2.9 per cent during the London G-20 summit from 2.5 per 
                    cent only a month earlier. The ISM, an index of American industrial 
                    production has risen to 53, up from the recession bottom of 
                    32 last December.<br>
                    <br>
                    The August job loss was only 216,000 down from a 740,000 low 
                    last January. Interest rates had plunged after Chairman Bernanke 
                    made it clear that the world’s central bank would not threaten 
                    the fragile recovery with a premature rate hike. The three-month 
                    Eurodollar rate is a mere 30 basis points lower than even 
                    Japanese money market rates.<br>
                    <br>
                    It is therefore not difficult to envisage higher index values 
                    in the next six months, though the sheer scale of money on 
                    the sidelines (liquidity in Wall Street jargon!) will limit 
                    corrections to short 5-10 per cent pullbacks, as we witnessed 
                    in July 2009. Yet it is important for investors not to gloss 
                    over the risks that could derail the market’s bullish momentum. 
                    The consumer, two thirds of the American economy, has been 
                    badly hit by the collapse in house prices, the rising job 
                    losses and the unwillingness of banks to extend credit to 
                    retail borrowers. Financial provisions and writedowns could 
                    make a mockery of the 2010 consensus earnings estimates.<br>
                    <br>
                    <b>Dangers ahead</b><br>
                    Corporate America has slashed costs and payrolls, yet sustained 
                    revenue, margin and profit growth is still nowhere near guaranteed. 
                    Obama’s health care reforms could mean tax hikes, bank loan 
                    books could be hit by losses on commercial real estate and 
                    sluggish consumer spending could risk a double-digit recession. 
                    Cautions guidance on 4Q earnings by bellwether companies and 
                    even an oil price surge to $80-100 per barrel due to a geopolitical 
                    shock in the Middle East or a collapse in the dollar are only 
                    some of the myriad risks that could destroy the bull run in 
                    the American stock markets in the next six months.<br>
                    <br>
                    It is important not to forget that the markets earnings multiple 
                    has almost doubled since last November, from eight to 15 times 
                    forward earnings. The stock market prices are now in a full 
                    throttle cyclical recovery while the bond market prices are 
                    in sluggish growth and easy money, the reason why the two-year 
                    Treasury note is only 93 basis points, down from 136 basis 
                    points only two months ago.<br>
                    <br>
                    With dollar interest rates at a generational low and index 
                    earnings expected to rise to $70 next year, the American stock 
                    market trades just below 15 times earnings. This is not excessive, 
                    though the market is technically overbought and investor sentiment 
                    (invariably a contrarian indicator) is far too bullish in 
                    its complacency.<br>
                    <br>
                    <b>Cautious investments</b><br>
                    The easy money has long been made in the stock market, particularly 
                    in speculative, high beta, busted financials like Citigroup 
                    and AIG, whose shares have quadrupled in valu<img border="0" src="images/img13.jpg" align="left" hspace="8" vspace="3" width="400" height="296">e. 
                    The rally was triggered by hedge fund covering their short 
                    sales in May, June and July as the market indices spiked higher. 
                    Pension funds, university endowments, insurance companies 
                    and sovereign wealth, no longer willing to allocate hundreds 
                    of billions of dollars in illiquid asset classes like private 
                    equity and real estate, have scrambled to buy shares in order 
                    not to underperform their index benchmarkets.<br>
                    <br>
                    Unlike the stock markets of the Gulf, the American stock market 
                    is dominated by institutional, not retail, investors. This 
                    does not prevent manias and bubbles, as the Silicon Valley 
                    tech/media/telecom in 2000-01 demonstrated.<br>
                    <br>
                    However, it does ensure that the herd instinct of institutional 
                    investors usually keeps the market averages in a rational 
                    valuation range – and 15 times 2010 earnings for the SP500 
                    index is not at all irrational. This is particularly true 
                    since the assets of money market funds, the high-octane fuel 
                    for liquidity driven stock market, are still in excess of 
                    $3.5trn, almost one-fourth the current market cap of the SP500 
                    index.<br>
                    <br>
                    As the indices have risen, it is imperative that investors 
                    rotate into defensive, high dividend yield shares with reasonable 
                    valuations and megacap status. American blue chips with dividend 
                    yields above four per cent include General Electric (industrial), 
                    Verizon Communications (telecom), Philip Morris (tobacco), 
                    Chevron (oil), Merck (pharmaceuticals) and MacDonalds (fast 
                    food).<br>
                    <br>
                    In addition, Coca Cola is a defensive global blue chip, with 
                    a dividend yield in excess of 3.5 per cent. The dividend yields 
                    of American corporates are, of course, lower than European 
                    blue chips like Total, Telefonica France Telecom, Vivendi, 
                    Munich Re, Nokia and Allianz, where five per cent and even 
                    six per cent dividends are possible. At a time when dollar 
                    and Euro money market rates will remain near zero, cash is 
                    not king and the case for defensive, high dividend equities 
                    is irrefutable.<b><br>
