                    <br>
                    </font><font face="Verdana" size="4"><b>Gearing Up<br>
                    </b></font><b><font face="Verdana" size="1">Omantel has been 
                    exerting efforts to come up with different and better ways 
                    to serve its customers. With competition looming ahead, it 
                    is only natural that the company should make such moves to 
                    satisfy its corporate customers, writes Visvas Paul D Karra<br>
                    </font></b><font face="Verdana" size="4"><b> <br>
                    </b></font><font face="Verdana" size="1">The Sultanate’s telecommunications 
                    market is <img border="0" src="images/img23.jpg" align="right" hspace="8" vspace="5">experiencing 
                    a new phase of exciting services with the liberalisation of 
                    fixed and mobile services. In the mobile market, there are 
                    two mobile operators along with two mobile resellers with 
                    various brands and services, while in the fixed line services, 
                    the awarding of a license to a second operator is expected 
                    to bring in a wave of competition for the country’s incumbent 
                    operator, namely Omantel (Oman Telecommunications Company).<br>
                    <br>
                    Omantel and Oman Mobile have been merged into one entity with 
                    a clear strategic positioning to make Omantel the leading 
                    telecom service provider and to have a strong stand during 
                    the fixed market liberalisation. There is a flurry of activity 
                    to synergise the strengths of both companies and offer the 
                    customer a better experience in the face of growing competition. 
                    <br>
                    <br>
                    Says Adil Ismail Al Raisi, vice president corporate customers 
                    unit, “If you ask me whether Omantel is geared up to face 
                    the expected competition in the near future then it is a definite 
                    yes. In anticipation of the expected competition and the liberalisation 
                    of the market, Omantel has initiated a process of introducing 
                    comprehensive and unique telecom services to its subscribers.”<br>
                    <br>
                    <b>Customer-centric focus</b><br>
                    With state-of-the-art technologies like an advanced Network 
                    Operation Centre (NOC) to manage the daily operations of its 
                    entire network, and organisational restructuring taking place 
                    within the process of the merger, it seems that Omantel has 
                    got the right people at the right places to take the company 
                    forward amidst the tremendous developments, which are taking 
                    place in the international telecom scenario.<br>
                    <br>
                    The company has become more dynamic and a more customer-centric 
                    organisation now. Says Al Raisi. “We are listening to our 
                    customers and offering solutions that fulfil their needs. 
                    For example, before 2005, Oman Mobile had very limited price 
                    plans for its consumer segment. To match the competition, 
                    we have introduced multiple price plans and packages to suit 
                    different segment needs, whether residential or corporate.”<br>
                    <br>
                    Rising through the ranks, Al Raisi, who was with Omantel since 
                    2003, became vice president corporate customers unit just 
                    a year ago and today he is managing a dynamic team whose sole 
                    aim is to satisfy the corporate customers by fulfilling their 
                    business needs. “To start with, we segmented our customers 
                    into meaningful groups that have similar needs for telecom 
                    solutions using hybrid approach that combines industrial segmentation 
                    with company potential size,” Al Raisi disclosed.<br>
                    <br>
                    <b>Segmented approach</b><br>
                    Al Raisi says that Omantel is listening to the voice of customers 
                    and the company is coming out with customised plans for different 
                    customer needs. He says that Omantel has really given heed 
                    to the customer’s feedback and has started segmentation in 
                    a structured manner. “We have done sector-wise segmentation 
                    like oil and gas, financial institutions, etc as they have 
                    different needs. For example, a bank may want a secure, reliable 
                    and cost effective network solution to connect its branches 
                    and ATM machines while an educational institution wants a 
                    reliable and cost effective high bandwidth Internet leased 
                    line to fulfil its educational needs; similarly, an international 
                    oil company might require an international private leased 
                    line to connect with their North America head office.” Al 
                    Raisi says.<br>
                    <br>
                    Besides this, Al Raisi also introduced account relationship 
                    management concept wherein one account manager would manage 
                    a basket of accounts within same market segments such as the 
                    industrial segment. Today, the company has dedicated account 
                    managers who will address all the needs of those companies 
                    right from mobile phone requirements to fixed line and Internet 
                    connections. So this way, the account manager becomes familiar 
                    with the sector as well, and would come up with innovative 
                    ideas in future. At the same time he would be able to serve 
                    his sector better. Having understood the market requirements 
                    with this strategy, Omantel was able to introduce new services 
                    and schemes to fit different business needs. One example of 
                    this is the newly introduced internet ADSL packages from starting 
                    from 512Kbps.<br>
                    <br>
                    Says Al Raisi, earlier the 512Kbps pricing plan was not available 
                    for corporate customers as the starting range was from 1Mbps 
                    but the market study revealed that the number of small and 
                    medium enterprises who actually want a 512Kbps is good enough 
                    to warrant the introduction of this plan. It may be noted 
                    that the SME segment is the largest segment of corporate customers 
                    for Omantel. Also, Omantel introduced effective September 
                    1, 2009, new ADSL plans with unlimited downloads that are 
                    suitable for companies with a larger number of employees with 
                    heavy download needs. For example, today 2Mbps ADSL is available 
                    with unlimited download and another plan of 2Mbps with limited 
                    10GB download but at less than half price. <br>
                    <br>
                    <b>Marketing strategy</b><br>
                    The marketing strategy to give the customer value proposition 
                    began from segmenting the market with different needs, says 
                    Al Raisi. Secondly, Omantel began to study different market 
                    needs, which are usually quantitative and qualitative. Around 
                    three months back, the company conducted an extensive market 
                    research of 100 corporate customers from different sectors 
                    to understand their Internet needs.<br>
                    <br>
                    It was found that first, there is a big demand for high bandwidth 
                    Internet solutions in hospitality segment; however, their 
                    demands are more sophisticated than other segments. Second, 
                    there were some customer segments who wanted unlimited downloads 
                    using WiFi through ADSL lines while others wanted to limit 
                    their Internet traffic but with lower prices. For example, 
                    hotels and coffee shops, wanted high speed Internet with secure 
                    WiFi solutions for their customer usage.<br>
                    <br>
                    <b>Corporate needs</b><br>
                    One of the important finding that came up during the market 
                    research was the demand for variety of connectivity solutions, 
                    says Al Raisi. Omantel already has a wide selection of connectivity 
                    solutions, which are suitable for different kinds of corporate 
                    customers. Today many banks as an example are using Omantel 
                    managed connectivity solution MPLS (Multiprotocol Label Switching) 
                    that provide reliability and cost effectiveness to connect 
                    their branches and ATM machines. <br>
                    <br>
                    Highlighting this, Al Raisi informed that Omantel has introduced 
                    MPLS for corporate customers who have to connect different 
                    points of operations in their line of business with their 
                    head quarter. “We manage the operations of all these points 
                    from our Network Operations Centre (NOC). The advantage of 
                    MPLS is that customers need not to worry about the actual 
                    operations and maintenance. The MPLS customers are given a 
                    portal where they see their network utilisation all over the 
                    country and upgrade and downgrade based on their usage. This 
                    way customers have better control of their network,” Al Raisi 
                    said.<br>
                    <br>
                    The MPLS network is widely used by the companies with multiple 
                    branches such as financial institutions, car dealers and also 
                    by various ministries. Continuing further, Al Raisi says that 
                    a number of customers have demanded high-speed wireless Internet 
                    and we were able to fulfil these needs through introwducing 
                    our state of the art 3.5G network. “We are at a healthy stage 
                    in the mobile segment. Introducing the 3.5G gave a big boost 
                    to customer satisfaction and today we see many people using 
                    our mobile broadband Internet through 3.5G network with a 
                    wide selection of price plans,” points out Al Raisi.<br>
                    <br>
                    Talking about the future, Al Raisi predicts that next wave 
                    of competition in Broadband Internet services will stimulate 
                    demand and lead to better offers for customers using different 
                    technologies ranging from ADSL, mobile broadband and other 
                    technologies. Introducing Internet resellers could lead to 
                    further demand stimulation by penetrating into the segments 
                    that are more price sensitive and underserved.</font><b><font face="Verdana" size="4"><br>
