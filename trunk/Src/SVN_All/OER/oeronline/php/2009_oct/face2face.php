		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">Two Way Street</font></b><br>
                    <font face="Verdana" size="1"><strong>His Highness Sayyid 
                    Khalid Hamad Hamoud al Busaidi, chairman and president, Oman 
                    Football Association shares his plans to professionalise football 
                    in Oman with Mayank Singh</strong></font><br>
                    <br>
                    <font face="Verdana" size="1"><strong style="font-weight: 400"> 
                    As Mohammed Rabeea, scored the winning goal in the finals 
                    of the 19th Gulf Gup between Oman and Saudi Arabia, in January 
                    this year<img border="0" src="images/img9.jpg" align="right" hspace="7" vspace="7">, 
                    the nation erupted in frenzied celebration. The love and passion 
                    for the beautiful game manifested itself like never before. 
                    Says His Highness Sayyid Khalid Hamad Hamoud al Busaidi, chairman 
                    and president, Oman Football Association (OFA), “We realise 
                    that to continue winning in regional championships we need 
                    to improve the quality of the football in the Sultanate and 
                    for this we need to transform our elite league into a professional 
                    league.” The FIFA (Fédération Internationale de Football Association) 
                    and the AFC (Asian Football Confederation) has guidelines 
                    for a professional league. The OFA is also looking at countries 
                    which have adopted professional standards and have succeeded 
                    in improving the quality of the game with the help of the 
                    private sector. <br>
                    <br>
                    The huge following that football enjoys as a sport across 
                    the globe means that economic benefits can be derived from 
                    an association with the game. Realising the commercial potential 
                    of the game, the OFA is also looking at moving in this direction. 
                    “If we want to continue winning the Gulf Cup or Asia Cup we 
                    need to bring in new players and the best way of doing this 
                    is by bringing in better coaches and better infrastructure. 
                    We cannot do this alone and have to do this in partnership 
                    with companies who believe in the value of the game and what 
                    it can bring to the society in terms of social, economic and 
                    financial benefits,” says Sayyid Khalid. <br>
                    <br>
                    </strong><strong>Professional norms</strong><strong style="font-weight: 400"> 
                    <br>
                    FIFA and AFC lays down a set of rules for becoming a professional 
                    league. The OFA is working on a commercial model like setting 
                    up a company which will become the clubs representative in 
                    the league. This will help clubs which do not have commercial 
                    acumen to import such modern knowledge and practices. Such 
                    companies can either be publicly owned or be privately held. 
                    The idea is to look at football as a business with a balance 
                    sheet, profit and loss account etc.<br>
                    <br>
                    Most sporting clubs in Oman have assets like land (running 
                    into thousands of square meters at prime locations) and human 
                    talent that would be valuable for any company. There are 18 
                    Omani players like Ali Habsi, Ahmed al Hosni, Mohammed Rabeea, 
                    Ahmed Hadid etc playing outside the Sultanate. Ahmed Hadid 
                    who plays in Saudi Arabia has a contract value of over $2mn, 
                    which shows the latent potential of such players. With the 
                    right kind of investments these leagues can produce better 
                    players which can in turn generate better revenues for such 
                    clubs. Revenues from television coverage constitutes the biggest 
                    source of revenue for football clubs globally. Clubs in Oman 
                    can capitalise on similar opportunities. “Our players are 
                    no different from Kaka, Ronaldo or Ronaldinho, it is just 
                    that these players were trained and groomed properly and their 
                    talent became an asset,” says Sayyid Khalid. <br>
                    <br>
                    There are various models that can be explored in this direction. 
                    To become a professional club the existing teams like Muscat 
                    Club need to set up a commercial entity like a Muscat Club 
                    LLC. Such a club can either be sold to an individual or it 
                    can invite potential investors to invest in it or it can generate 
                    revenues from the clubs fund base. All the details of the 
                    company in terms of its governance structure, shareholding 
                    pattern and capital base also needs to be formalised. The 
                    OFA is seeking help from the English Premium League, the most 
                    advanced professional league in the world to help it in such 
                    an exercise. The English Premium League has accepted the offer 
                    and will be assisting the OFA’s efforts. <br>
                    <br>
                    A formalisation of the marketing aspects is another important 
                    aspect – are such clubs going to concentrate on asset development 
                    (like people development), merchandising or will they focus 
                    on projects like property related development like hotels, 
                    accommodation for players, restaurants etc. All such businesses 
                    will carry the clubs name. OFA is organising a conference 
                    in October in which it will bring in key decision makers from 
                    the private sector to share with them the values of the game 
                    and the opportunities that football holds for the Sultanates 
                    economy. <br>
                    <br>
                    </strong><strong>Evincing an interest </strong> <strong style="font-weight: 400"><br>
                    A number of companies like Nissan (Suhail Bahwan Automobiles), 
                    Shell Oman Marketing and Oman Mobile are already associated 
                    with the game. “The value that these companies are getting 
                    out of this association is huge. In the past we used to go 
                    to companies asking for charity, but today our approach is 
                    different, we state that these are the returns that your company 
                    will get as a result of the exposure that football gives you,” 
                    says Sayyid Khalid. Thus football is a win-win situation for 
                    the private sector and the clubs.<br>
                    <br>
                    The OFA has been approached by a number of companies to be 
                    either key sponsors or potential partners. The association 
                    is also coming up with new products like women’s football, 
                    yo<img border="0" src="images/img10.jpg" align="left" hspace="7" vspace="7">uth 
                    championship, beach football, under 12, 15, 17 and under 21 
                    years. These formats catering to various segments of the Omani 
                    Society will in turn attract the right partners for each of 
                    these potential products. In 2008 the OFA closed its accounts 
                    with more than one million rials in sponsorships. Emboldened, 
                    the association is targeting a better figure in 2009. A sign 
                    of the financial potential that football as a game has in 
                    Oman. <br>
                    <br>
                    There are 43 football clubs in Oman. The existing Oman Mobile 
                    league is a semi professional league. These clubs work as 
                    a social entity under the supervision of the ministry of sports. 
                    Their source of revenue is either government support or internal 
                    initiatives like developing property. The OFA also gives them 
                    financial support out of the partnership agreements that it 
                    has with Oman Mobile and Nissan. “Any money that we generate 
                    from sponsorships, partnerships and media coverage will be 
                    routed back to these clubs.” The objective is to have 12 clubs 
                    in the professional league by 2012. Once the professional 
                    league is in place, a company will be set for its management. 
                    This company will own the rights, formulate the corporate 
                    governance structure for the league and look into marketing 
                    and sponsorship issues. FIFA regulation requires a professional 
                    league to be managed by a company and clubs need to be companies 
                    as well. Says Sayyid Khalid, “Our vision is to transform football 
                    into an industry and it is something that we have been working 
                    on for the last two years.” And with the right leadership 
                    in place, Oman can look forward to many more winning goals 
                    in future.</strong></font><br>
