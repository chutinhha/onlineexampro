                    </font><b><font face="Verdana" size="4">First Things First</font></b><font face="Verdana" size="1"><br>
                    <b>Jack Perkowski, a keynote speaker at the Leaders in Dubai 
                    Busines</b><b>s forum speaks to OER about the economic crisis, 
                    poverty alleviation and climate change<br>
                    </b><br>
                    <img src="images/img47_new.jpg" width="120" height="160" hspace="8" vspace="6" border="0" align="right"> 
                    Jack Perkowski has lived in Beijing since 1994 and is the 
                    founder of JFP Holdings, a merchant bank for China, the founder 
                    and former CEO and Chairman of ASIMCO Technologies, one of 
                    the largest automotive components manufacturers in China. 
                    Jack is the author of “Managing the Dragon: How I’m Building 
                    a Billion Dollar Business in China.” He is a keynote speaker 
                    at the 2009 Leaders in Dubai Business Forum from October 26 
                    to 28 at Dubai International Convention Centre.<br>
                    <br>
                    <b>The banking system failed last year all around the world, 
                    what’s next for financial institutions?</b><br>
                    There was a comprehensive failure of the system, no doubt, 
                    and the failure was multilayered. Regulators did not play 
                    their part in regulating, bankers were selling what proved 
                    to be risky financial products internationally. These products 
                    were over complicated. The global economic system was over 
                    leveraged, especially in the US. As a system, the capitalistic 
                    model has a tendency to go to excess, and with the regulatory 
                    void that prevailed over the past years, there was bound to 
                    be a correction. This is a major correction and getting out 
                    of this slump will be painful. There will surely be more regulation 
                    going forward and obtaining leverage will be made more difficult. 
                    I think that financial institutions are a vital element in 
                    the economic business cycle. Their role is essential in driving 
                    growth, so they will remain in essence, fulfilling their role 
                    as intermediaries, but with far more restrictions and regulations.<br>
                    <br>
                    <b>How big a threat to the world do you believe global warming 
                    is and are government’s doing enough to address the problem?</b><br>
                    I am not convinced about global warming. I always have a problem 
                    with people coming up with simplistic ideas about very complicated 
                    issues. Ecosystems are very fragile and are constantly in 
                    flux, and small changes in one system will affect the other. 
                    I believe that the stres<img src="images/img46.jpg" width="150" height="137" hspace="8" vspace="6" border="0" align="left">ses 
                    being caused are due to the fact that today, we have one billion 
                    people living in the Americas and Europe, consuming and living 
                    at high levels. We also have 5.7bn people in the developing 
                    world who want to consume and live at that same level. This 
                    is a resource issue. The problem is that when such subjects 
                    get politicised, the experts, the great minds of our time, 
                    who should be left alone to solve them, also get politicised.<br>
                    <br>
                    <b>What are the biggest challenges facing people who wish 
                    to start a company in China today?</b><br>
                    China is very easy to do business in one sense. With the economic 
                    zones, you can incorporate and set up a company in 30 days. 
                    China has 85mn private enterprises, more than three times 
                    the number that exist in the US. The challenges are in recruiting 
                    and managing management teams and accessing finance. China 
                    does not have an efficient capital distribution system, and 
                    most of the financing goes to government owned companies.<br>
                    <br>
                    <b>Will the financial crisis slowdown climate change and poverty 
                    alleviation work?</b><br>
                    The obvious priority now is on the economy and not climate 
                    change or poverty. I am not a believer in the whole climate 
                    change argument anyway. Poverty alleviation, I believe can 
                    be tackled without direct donations. Entrepreneurship should 
                    be encouraged by infusing technology into developing countries. 
                    I believe that we have underestimated the creativity of developing 
                    countries and their people. </font> 
