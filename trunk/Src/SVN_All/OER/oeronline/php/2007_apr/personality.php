<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<span style="font-size: 14pt"><br>
</span><font size="4" face="Verdana">‘Don’t lose sight of the
human element’</font></b><font face="Verdana" size="1"><br>
<b>Sameer Al Awam, MD and COO, Muscat Eye Laser Centre, is a firm believer in the 
primacy of human resource behind the success of any business,
reports Sunil Kumar Singh</b><br>
<br>
<img border="0" src="per.jpg" width="300" height="206" align="left" hspace="6" vspace="4">For Sameer Al Awam, life is full of surprises __ he tries to live without the 
fear of failure. His conviction in doing things differently and his courage to 
follow the untrodden path are the prime movers of his life. This most probably 
contributes to his success and his forays into motivational speaking, teacheing, 
consultancy, IT, scuba diving, construction, human resource management and more.<br>
<br>
Ask Al Awam, Managing Director and Chief Operating Officer of Muscat Eye Laser 
Centre, which completes five years of operation in Oman next month, what’s the 
secret of his success, and he quips: “I always give the credit of whatever 
success I have achieved to my people, who I always believed in. For me, my 
employees have always been the vital cogs in the wheel for organisational 
success. Employees are not merely a statistical figure. Rather, they’re assets 
for the company. But often, when companies grow in size, they lose sight of this 
fact.”<br>
<br>
<b>People come first<br>
</b>Sameer Al Awam, a management degree holder from Austria, hails from Qatar but 
has made Oman his second home since 2002, the year he set up Muscat Eye Laser 
Centre along with Dr. Maria Clara Arbelaez, now Medical Director of the centre.<br>
<br>
Asked what attracted him to Oman, Sameer — who has an established business in 
Qatar, his home country — says, “The most fascinating aspect about Oman is its 
people, who are decent and honest. Secondly, I and my family love the serene 
natural beauty of the Sultanate. Whenever we take a break from our busy 
schedule, we like to see its natural wonders — the beaches, mountains, wadis 
etc. Thirdly, I found that in Oman there is a well organised and peaceful 
environment for doing business. In addition, there was lack of an advanced eye 
laser treatment facility too.”<br>
<br>
A firm believer in ensuring personalised relationships between the management 
and its employees as well with its customers, Sameer feels that the people one 
works with are the prime factors behind one’s success. And if a business 
organisation takes human resources as a liability, it’s doomed. As he reasons, 
“You have to look after your people and make them realise that they belong to 
the business, and the business to them. Simply put, an organisation is like a 
body and its human resources are its vital organs. If you look after your organs 
well, you’ll be bestowed with a healthy body.”<br>
<br>
<b>Rich experiences</b><br>
Sameer’s CV speaks of a successful track record in business management and 
consultancy, and he always made it a point to stick to the principles of 
management he learnt from life. In 1980, he set up a firm of consultants in 
Qatar to service private and government sectors in terms of designs, 
architecture and surveys.<br>
<br>
Between 1980 and 2002, his firm secured several construction projects such as 
residential offices, government facilities and schools — the French School in 
Qatar, Qatar Academy School and the American School. That’s not all. His passion 
for scuba diving led him to set up a company — Pearl Divers — in the early 
1980s, a five-star recreational diving centre in Qatar.<br>
<br>
He also established Tech International Marine Services, a marine services 
consultancy firm specialising in commercial diving which was certified by 
organisations like DNV (Det Norske Veritas), ABS (American Bureau of Shipping) 
and Lloyd’s. The company’s main operations included underwater services, 
surveying, underwater construction, testing, salvaging, servicing oil rigs and 
ocean vessels.<br>
<br>
After having established himself as a successful consultant, he felt it was time 
for him to enter the corporate boardroom — as a partner in the Saudi company 
Heinsco, which specialises in structural improvements and non-destructive 
testing for oil and gas.<br>
<br>
<b>Life’s a perfect mentor</b><br>
Perhaps Sameer’s rich experiences in the nuances of human resources management 
stem from a career in diverse fields. He started working from his high school 
days, a fact which many will consider unusual. After finishing high school, he 
took up teaching. For him, more than money, it’s that streak of doing things 
differently that has inspired him all along. <br>
<br>
“I have been very unconventional in leading my life and wondered how people 
finish their high school at 17-18 years of age, then go for higher education, 
get into a job and so on. Initially, I thought of myself as being unlucky 
because I couldn’t enjoy life like other kids of my age. But gradually I 
realised that I was not so unlucky, because my unorthodox way of living gave me 
unconventional exposure. If I was ever to relive my life, I will not regret a 
thing,” Sameer explains.<br>
<br>
When asked what keeps him motivated, he replies, “The harder you work, the more 
you’ll enjoy the result. I’m also a firm believer in patience — and diving 
teaches me a lot about being patient. Besides, I also believe in taking 
pre-emptive actions — that is, anticipating the problem and finding solutions 
immediately. I also believe in hard work and tolerance. I always tell people to 
believe in their mission and never consider themselves unlucky, because life is 
full of surprises.” <br>
<br>
At the end of the day, Sameer still believes he has miles to go. He makes sure 
that the Eye Laser Centre is equipped with state-of-the-art technology and 
highly skilled personnel to draw patients from all across the globe. For this, 
he ensures regular upgrading of the employees’ skills.<br>
<br>
In an effort to instill a sense of virtue and values in his employees, his 
organisation also undertakes charity work periodically besides holding seminars 
and workshops on motivation and workplace coordination.</font></p>

