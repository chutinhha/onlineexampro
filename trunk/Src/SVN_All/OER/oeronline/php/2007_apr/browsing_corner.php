        <p align="justify"><b><font face="Verdana" size="1">Title: Al Waleed, 
        Businessman Billionaire Prince<br>
        Author: Riz Khan</font></b><br>
        <br>
        <font size="1" face="Verdana">
        <img border="0" src="browse.jpg" width="196" height="247" align="left" hspace="6" vspace="4">Meet His Royal Highness Prince Al Waleed. A Saudi royal and business 
        magnate, he has earned wide acclaim due to his impeccable style of 
        working and his drive for success, besides his invaluable efforts in 
        promoting human rights, education and many other humanitarian 
        activities. So what exactly has the Prince done right that has earned 
        him the nickname of the ‘Arabian Warren Buffett’? What is this 
        discerning business kingpin’s route to wealth? This book explores the 
        many facets of the Prince’s life, chronicling it right from his early 
        days to his journey through his youth and on to business.<br>
        <br>
        HRH, Prince Al Waleed Bin Talal Bin Abdulaziz Alsaud was born to 
        Princess Mona El Solh and Prince Talal Bin Abdulaziz Alsoud on March 7, 
        1955. His mother, Princess Mona, of Lebanese royalty, and his handsome 
        Saudi father, Prince Talal, split when the young Prince Al Waleed was 
        five years old. The young prince then went on to spend his childhood in 
        Beirut with his mother. A rebel from his early days, the Prince was, 
        however, extremely close to his younger brother, Prince Khaled and baby 
        sister, Princess Reema. Prince Al Waleed pursued a Bachelor in Science 
        degree in Business Administration from Menlo College in the US in 1979 
        and obtained a Masters in Social Science from Syracuse University in 
        1985. <br>
        <br>
        <b>Financial Acumen</b><br>
        The Prince’s rise to fortune began with his return to the Middle East 
        in1979. Armed with his Business Administration degree, he was no longer 
        a rebel without a cause. Instead, this suave and punctilious person had 
        his first tryst with business, investing in the construction and real 
        estate sectors at a time when oil prices were hitting the roof and 
        business was booming. The Prince set new standards, being the first to 
        moot the idea of mergers. A man of quick decisions, the region’s banking 
        sector soon caught his fancy. Accentuating his path to success was the 
        Prince’s alliance with Citibank in which he put in sizeable investment. 
        His insights, his guts and his business sense are reflected in his 
        ability to back winners. Prince Al Waleed’s investment portfolio, 
        centred around banking, real estate, hotels, media and technology, has 
        earned him the adulation of the legendary Warren Buffett, ensuring the 
        Prince a slot in the Forbes’ Top 10 list of wealthiest investors in 
        2004.<br>
        <br>
        The Prince’s business house, Kingdom Holding Company is headquartered in 
        his hometown, Riyadh, and takes care of all his Saudi-based investments. 
        The city is also home to his aesthetic dream project, Kingdom Tower, a 
        modern and ambitious delight in glass and metal with a ‘sky bridge’ 
        walkway across the top, offering an unrivalled view of Riyadh.<br>
        <br>
        <b>Global Vision</b><br>
        Whether it’s his media deal with the Italian leader, Berlusconi, or his 
        investments in Korea, or his business acumen in buying and renovating 
        France’s George V Hotel, Prince Al Waleed has made a mark across the 
        globe. There have been lesser successful moments too, like his 
        association with the Disney theme park just outside Paris. However, 
        these seem like mere blips to an otherwise unblemished success path that 
        the Prince has charted. <br>
        <br>
        The softer side of the Prince is evident through his associations with 
        the UNICEF, his charities, and his active support and involvement in 
        education, medical treatment and women’s rights. His son, Khaled, and 
        daughter, Princess Reem, form the centre of his universe, as he manages 
        to juggle time and balance his children’s needs with his business. A 
        religious person and a striking example of a mix between the Middle East 
        and the West, Prince Al Waleed easily slips into different roles as his 
        feisty presence lights up many worlds. It is his will power, passion for 
        excellence and analytical mind that leave a long lasting impression. 
        Read ‘Al Waleed Businessman Billionaire Prince’ to capture the myriad 
        images of this regal persona.</font></p>
