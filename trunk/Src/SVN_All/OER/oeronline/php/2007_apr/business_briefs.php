</font><font face="Verdana" size="2" color="#CC3300"><b>Gulf Merchant Group to launch new bank in Oman</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bus4.jpg" width="125" height="146" align="left" hspace="6" vspace="4">Gulf Merchant Group (GMG), a leading investment banking and asset management 
firm, has received the initial approval for a full banking licence from the 
Central Bank of Oman to operate a new retail and commercial bank. Initially 
capitalised at a minimum of RO50 million, the Oman Merchant Bank (OMB) is likely 
to be launch by the end of 2007, following an initial public offering (IPO) on 
the Muscat Securities Market. Key factors driving GMG’s decision to launch the 
bank are the strong economic performance of the Omani economy, de-regulation of 
key sectors in the economy by the government, upcoming privatisation, 
much-needed broadening of private sector ownership, FDI inflows, strong 
population growth, and expected changes in private wealth management.<br>
<br>
HH Al Sayyid Asaad Bin Tariq Al Said, Chairman of OMB, said: “We are seeing a 
gradual opening of the Omani economy that is creating an investment-friendly 
environment to attract the much-needed FDI into the Sultanate. The Government of 
Oman is also committed to major infrastructure developments that will 
accommodate the needs of a young and growing population in line with the vision 
of His Majesty Sultan Qaboos Bin Said. OMB’s focus on corporate and investment 
finance means that the bank will be well poised to capitalise on the 
opportunities created by these important developments.”<br>
<br>
Nabil Maaloul, CEO of GMG said: “We are delighted to be given this opportunity 
to participate in the growth of the Omani economy by providing vital corporate 
and investment banking services. Furthermore, Omani nationals will be able to 
work and invest in the bank and its future. We are also considering a secondary 
listing on a reputable international exchange such as London or Dubai’s DIFX to 
allow regional investors to participate in a bank we believe will become an 
important player in the Omani economy.”<br>
