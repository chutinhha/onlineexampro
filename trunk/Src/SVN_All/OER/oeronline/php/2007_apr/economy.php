<p align="justify">
<b>
<font face="Verdana" size="4"><br>
So, what do you want from FDI?</font></b><font face="Verdana" size="1"><br>
<b>Oman has to find niches for foreign investment that will solve its most 
immediate need to increase the involvement of the local workforce in the economy<br>
</b>
<br>
<img border="0" src="eco.jpg" align="right" hspace="6" vspace="4">Foreign direct investment (FDI) has always bedevilled the Middle East. Despite 
the massive outlays that are offered for the oil and gas sector, the state of 
FDI in other sectors has traditionally been low.<br>
<br>
All of Oman’s regional partners in the GCC are making the competition for FDI 
tougher, in part because of the similarity of potentials on offer. To mark 
itself out from the crowd, Oman needs to find a series of niche industries and 
sectors that it can leverage and ensure that the effect of FDI is more broadly 
felt throughout the country.<br>
<br>
The GCC is the primary regional trade agreement that Oman is a part of, though 
other trade agreements, such as the FTA with the US, are also influential on the 
flow of FDI. However, in these times of FDI encouragement, the question has to 
be asked: “Does FDI actually encourage economic growth?”<br>
<br>
<b>The ’spillover effect’</b><br>
Consensus on the relationship between economic growth and increased levels of 
FDI is not to be found. However, there are a number of interesting attempts to 
link the two. Generally, such attempts point out the improvements in technology, 
efficiency and productivity that come following the arrival of FDI. Another 
concept is that an economy in receipt of FDI benefits from what is called a 
“contagion” effect, as practices and technology from foreign firms become 
absorbed into the local business environment.<br>
<br>
Such a contagion effect is also called a “spillover”, as a local firm begins to 
adopt the same practices and technology as the new foreign arrival in the 
country. Another form of spillover can occur when local companies must better 
use their own resources and technology in a more efficient manner, or bring in 
such things to be able to maintain a competitive edge with the foreign entrant. 
Equally, the foreign firm may require services and inputs not available on the 
market, thus inspiring local businessmen to begin supplying them. A different 
form of spillover can occur when the local employees hired by the foreign firm 
leave and join other local companies or start their own, and thus bring with 
them the techniques learnt previously. Of course, these are not the only ways 
that the entrance of foreign firms, and hence FDI, into a market can improve its 
growth pattern, and economists are likely to argue over the effects of FDI for 
many more years to come. <br>
<br>
Some feel that the entry of foreign firms can stifle local manufacturers and 
force them out of business, though this may be an initial result of foreign 
investment targeting countries with structural inefficiencies. A good testing 
ground for such theories has been Eastern Europe following the fall of the 
Berlin Wall. At first, the more inefficient industries either collapsed or were 
rapidly privatised to local and foreign investors alike. While there was an 
initial loss of jobs, as well as a decline in national ownership of production 
units, the results seen today in the mostly EU member Eastern European bloc are 
indicative of what foreign investment can do to turn economies around. Of 
course, Eastern Europe had one asset that other countries looking to benefit 
from FDI do not always have: a low wage, highly educated workforce. Having such 
conditions improves the spillover effects of foreign investment as the magnitude 
of the gap between the foreign company and investment host are that much lower.<br>
<br>
This magnitude of difference between technology and the local ability to absorb 
and replicate it can also work in the opposite direction, as has been the case 
in the Gulf and other oil producing states. The injection of FDI into such 
activities has had a limited spillover effect into local economies, as trained 
staff are often externally sourced, few locals are in a place to play a role in 
such operations, and the distance of production facilities from the local 
population may also act as a barrier to interaction. This is normally referred 
to as an “enclave situation”, in that the benefits of FDI are largely felt 
within the immediate area of investment. This is not to mean that there are no 
positive benefits from this form of FDI targeting, as revenues derived from such 
production units are then passed over to, more generally, the state, which then 
uses these funds to distribute them to the public. The limitation of this form 
of FDI is obvious in its creation of unwieldy bureaucracies that often do not 
improve local education standards and technical know-how up to the point where 
further spillover effects from the enclave investment can be felt. <br>
<br>
In an attempt to promote technological and practise spillover, most countries 
have adopted workforce nationalisation schemes to varying effect. In Saudi 
Arabia, attempting to match the right sort of workers with the needs of the oil 
industry is somewhat hampered by the low number of engineering graduates 
emerging from the state education system, although moves to improve this 
situation are being taken. Imposed minimums on workforce nationalisation can 
have the effect of installing unsuited staff into occupations, and thus forcing 
a company to hire another foreign worker to replicate what the unskilled worker 
cannot achieve. In a sense, everyone loses in this situation, and this is the 
problem that many are attempting to grapple with as FDI is sought, and a balance 
is found between the needs of the local population and those of the foreign 
investor. <br>
<br>
Equally, another barrier to spillover from such enclave situations can also 
occur because of the capital gap needed to establish facilities. The combination 
of technical and financial deficiencies makes it incumbent on the seeker of 
foreign investment to accede to demands made by the external investor. This 
situation is mostly the case in poorer countries that are unable to leverage 
their resources in profitable ways without external support, and was a stage 
that the Gulf passed through in the first days of the oil industry, and which 
decreased over time especially following the wave of oil asset nationalisations 
in the 1970s. <br>
<br>
<b>Horizontal or vertical?</b><br>
However, one thing that is of importance to Oman is the type of FDI that is 
coming into the country. FDI can be classified into two basic types: horizontal 
and vertical. The horizontal form often seeks to exploit markets within the 
local economy. If the cost of supplying a product to a local economy is too high 
or not achievable because of physical or legal obstructions, foreign companies 
may choose to relocate part of their production to an external market. For 
example, if it costs less to make a tin of jam locally than to import it, and 
the conditions are right for production in that local economy, then a firm may 
choose to start up in a country. Another form of horizontal FDI could arguably 
be that of real estate investment, especially in the Gulf where the rise of 
large constructors going international is looking to tap into different markets 
to gain access to them and spread their investment risk profile. Given the rise 
of free trade areas and the like, much of the process of horizontal investment 
in the form of “duty jumping” is slowing internationally and has been shifted 
more towards the next style of FDI: vertical.<br>
<br>
Vertical-style FDI is often known as “production cost-minimising”, since it 
involves moving a part of the production chain to an external market where input 
and production costs are lower. This is the sort of FDI that seeks to find out 
the differences in commodity prices and utilise them. A good example in the Gulf 
is where FDI projects involve the use of hydrocarbons in downstream activities 
due to the price difference between utilising them locally or shipping them and 
using them externally. <br>
<br>
Such differentials can be substantial, such as in the Royal Commission of Jubail 
and Yanbu in Saudi Arabia, where gas prices are up to a tenth what they would be 
on the international market. For a manufacturer in need of downstream stock 
material, establishing facilities in such a place represents a huge profit 
advantage.<br>
<br>
<b>Conclusion</b><br>
So, how does all of this affect Oman? As a country that has in the past received 
a lower share of FDI, Oman has to begin to ask itself what benefit it wants from 
foreign investment. It has to find niches that will solve its most immediate 
need, which is to increase the involvement of the local workforce in the economy 
and decrease the enclave effect that much previous investment has occasioned in 
the economy. Oman also has to increase its investment in the educational 
standards of its own population to make them better match up with the needs of 
foreign investors and improve the spillover effects into the local economy. <br>
<br>
A more highly educated workforce avoids not only a reliance on foreign labour, 
but also means that the number of options Oman is able to present to possible 
foreign investors is much greater than other local rivals all seeking to target 
a small number of sectors and firms.<br>
