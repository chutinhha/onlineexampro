<font face="Verdana" size="1">
<br>
<b>Outward FDI: Petrodollars Boost
Investment<br>
</b>Surging oil prices and increased foreign exchange reserves have made West Asia 
an important source of FDI outflows, notably by the State-owned investment firms 
of oil-exporting States such as Kuwait, Saudi Arabia and the United Arab 
Emirates. In 2005, outward FDI flows rose to US$16 billion, compared to US$7 
billion in 2004. For the first time since 1990, outflows from the region 
surpassed those from ASEAN member States. This was mainly because outward 
cross-border M&amp;As, mainly in services, increased 12-fold. The oil-producing 
countries are increasingly investing abroad, specially in services and 
oil-related manufacturing. This phenomenon of “petrodollar recycling” is in 
sharp contrast to the one in the 1970s and 1980s, when portfolio investment 
dominated. <br>
<br>
Private-equity and institutional investors from West Asian countries have 
invested in various areas, sometimes through large-scale investments. For 
example, Kingdom Holding, the Saudi State owned company which has been an active 
private-equity firm since the 1980s, targets not only blue-chip shares and 
luxury hotels in developed countries, but also emerging firms in developing 
countries, including in Africa. In Bahrain, Investcorp and Arcapita Bank use 
their private equity arms to purchase majority shares in companies in Europe and 
the United States. <br>
<br>
<b>Sectoral Trends</b><br>
FDI data for the region by sector are scarce. However, available data suggest 
that West Asia’s inward and outward FDI flows are highly concentrated in the 
services sector. FDI in manufacturing has also been taking place, for instance 
in textiles, as well as in areas related to oil and gas. <br>
<br>
In the case of inward FDI, the shift towards services is in response to 
increasing liberalisation, whereas the rise of FDI in manufacturing is mainly in 
downstream activities (part of manufacturing) since FDI in upstream activities 
in the energy industries is not allowed in most West Asian countries. In 
response to increasing global demand, countries are trying to attract FDI in 
downstream activities related to natural resources to increase production and 
improve productivity through advanced technologies.</font></p>
