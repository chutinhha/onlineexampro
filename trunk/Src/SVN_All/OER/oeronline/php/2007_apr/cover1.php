</font><b><font face="Verdana" size="4">Changing Dynamics</font></b><font face="Verdana" size="1"><br>
<b>Drawing from UNCTAD’s World Investment Report 2006,
OER analyses the trends in
outward and inward foreign
direct investment with special
reference to West Asia</b></font></p>
