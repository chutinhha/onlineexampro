<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>Inward FDI: Unprecedented Rise<br>
</b>FDI flows into the 14 West Asian countries rose by 85 per cent in 2005, reaching 
a record US$34 billion. Similarly, the share of West Asia’s inward FDI in total 
inward FDI in Asia and Oceania was the highest since 1985-over 17 per cent. Its 
share in all developing countries’ inward FDI also increased, from 7 per cent in 
2004 to 10 per cent in 2005. Again, FDI as a percentage of gross fixed capital 
formation (15 per cent) surpassed that of Asia and Oceania as well as of all 
developing countries for the first time in 2005.<br>
<br>
Several factors explain this high growth. First, the region experienced strong 
economic growth, spurred by production increase due to high commodity prices. 
During the period 2003-2005, the GDP growth rate averaged 7.4 per cent in eight 
West Asian countries, compared to 5 per cent for the developing world as a 
whole. This raised the region’s GDP per capita, which was already high. 
Large-scale greenfield investments and cross-border M&amp;A deals were facilitated 
by the booming local economies and prospects of continuing high prices of oil 
and gas. <br>
<br>
FDI in downstream activities in the oil and gas industries has also been spurred 
by a rise in world demand. Also, the business climate has become more favourable 
following the good performance of the Gulf Cooperation Council (GCC) members. 
Again, liberalisation efforts continued to gather momentum in the region, with 
the increasing privatisation of services (telecommunications, water and energy 
supply, and banking). <br>
<br>
Finally, foreign affiliates in the region improved their performance. This sent 
a promising signal to potential investors. FDI inflows to West Asia in 2005 were 
spread unevenly among the region’s economies, being concentrated in Saudi 
Arabia, Turkey and the United Arab Emirates. The Islamic Republic of Iran and 
Yemen failed to attract more inflows, mainly due to increasing geopolitical 
uncertainty.<br>
<br>
The United Arab Emirates was the largest recipient of FDI in West Asia, with a 
record US$12 billion mainly going to the country’s 15 free trade zones. Turkey 
followed, with a few mega deals that included the privatisation of Türk Telekom 
and two deals in the banking sector. <br>
<br>
Although developed countries continued to be the main sources of FDI, the year 
also increasing inflows from the developing world. It is noteworthy that such 
FDI is increasingly intra-regional, specially in services, and is concentrated 
in a few countries. Cross-border M&amp;As in West Asia saw a historical increase 
from US$0.6 billion in 2004 to US$14 billion in 2005. Intra-regional M&amp;As 
accounting for 65 per cent of the total value and 30 per cent in terms of 
numbers played an important role in this growth.</font></p>
