<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Globally, in 2005, foreign direct investment recorded a growth for the second 
consecutive year. Inflows increased substantially in 2005 to reach US$916 
billion, a growth of 29 per cent and this on the back of the 27 per cent growth 
recorded in 2004. Inward FDI grew in all the main sub-regions, in 126 out of the 
200 economies covered by the United Nations Conference on Trade &amp; Development 
(UNCTAD). In some regions, the growth was unprecedented. Nevertheless, world 
inflows remained far below the 2000 peak of US$1.4 trillion. <br>
<br>
During 2005, global FDI outflows amounted to US$779 billion (this variance 
between inflows and outflows is due to differences in data reporting and 
collecting methods). Developed countries remained the leading source of such 
outflows. In 2005, the Netherlands reported outflows of US$119 billion, followed 
by France and the United Kingdom. However, there were also significant increases 
in outward investment by developing economies, led by Hong Kong (China) with 
US$33 billion. Indeed, the role of developing and transition economies as 
sources of FDI is increasing. Negligible or small until the mid-1980s, outflows 
from these economies totalled US$133 billion last year, corresponding to some 17 
per cent of the world total.<br>
<br>
Similar to trends in the late 1990s, the recent upsurge in FDI reflects a 
greater level of cross-border mergers and acquisitions (M&amp;As), especially among 
developed countries. It also reflects higher growth rates in some developed 
countries as well as the strong economic performance of many developing and 
transition economies. M&amp;As rose by 39 per cent in value terms in the first half 
of 2006 over the same period of 2005. But the current cross-border M&amp;A boom is 
partly due to the activities of private equity and hedge funds. However, if FDI 
growth is due to such investments, rather than on investments by transnational 
companies, it is not certain how long it will continue.</font></p>
