<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><br>
<b>Inspiring Confidence<br>
</b>Looking at prospects by region, a number of recent surveys confirm the growing 
importance of the Asian economies, in particular West Asia, as FDI destinations. 
The FDI Confidence Index shows unprecedented levels of investor confidence in 
emerging markets, led by China and India. Choosing among emerging markets, 
respondents to the 9th Annual Global CEO Survey carried out by Pricewaterhouse 
Coopers said they are investing the most in China, followed by India, Brazil and 
the Russian Federation. Other fast growing economies (Indonesia, Mexico and 
Turkey) are also at the top of the list of the most preferred locations. FDI 
prospects for Eastern European nations are also bright, but are less favourable 
for Africa and Latin America as a whole. Also, AT Kearney’s FDI Confidence Index 
reveals that investors have lost confidence in Western Europe other than the 
United Kingdom due to increasing competition from emerging markets and 
protectionism.<br>
<br>
<b>FDI in West Asia</b><br>
West Asia saw a historic growth in FDI flows in 2005-both inward (US$34 billion) 
and outward (US$16 billion). The growth rate of inflows in the region was the 
highest in the developing world for the second consecutive year. Similarly, 
outflows from the region more than doubled. Economic growth, high global oil 
demand, a favourable investment environment and economic diversification efforts 
were the main factors behind this growth. This is even though there were some 
concerns about geopolitical uncertainties in some parts of the region.</font></p>
