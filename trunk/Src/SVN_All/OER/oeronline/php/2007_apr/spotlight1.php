<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">
‘The Sultanate has a positive business climate’<br>
</font></b><font face="Verdana" size="1"><br>
<b>Tiemen Meester, CEO of the Port of Salalah, shares his insights on the 
critical advantages of the port with Sunil Kumar Singh. Excerpts…</b><br>
<br>
<b>
<img border="0" src="spot3.jpg" width="175" height="214" align="left" hspace="6" vspace="4">It 
is said that the Salalah Free Zone would be an engine for economic development 
in the region of Dhofar and the Sultanate as a whole. Can you explain how?</b><br>
It is our understanding that a couple of petrochemical businesses are already 
being set up in the Free Zone. We also see great potential for other 
manufacturing units and traders who could use the ideal geographical location to 
distribute products to other ports at the Indian Ocean Rim using the 
containerised services as well as bulk and general cargo vessels, including 
dhows.<br>
<br>
The Port of Salalah has always been a strong supporter of the Free Trade Zone 
development in Salalah. It is located directly behind the Port of Salalah, and 
businesses there will have access to ample shipping capacity – both bulk and 
containerised – that offers immediate connections to the global markets apart 
from the two billion consumers at the Indian Ocean Rim. Also, Salalah airport is 
only a 20-minute drive away, so there could also be great opportunities for the 
development of sea/air transportation links for time-sensitive cargoes. People 
and organisations in Dhofar, Oman and the world have a great opportunity to 
develop profitable businesses in the Salalah Free Zone. It is also about 
creating value-added jobs. At the Port of Salalah, we are pleased to have about 
1,000 Omani colleagues, but Dhofar has a growing young population that needs 
jobs. With a strong free zone development, there will be more opportunities for 
training, education and careers that help us make a difference.<br>
<br>
<b>Which industries have benefited from the development of the Salalah port?<br>
</b>The Port of Salalah, through the trickle-down effect, is a main contributor 
to most businesses in and around Salalah. We are the largest employer in the 
region, so our people generate a lot of business when they build homes, buy cars 
or groceries. Several port-related businesses have also been developed in and 
around the port, and we always use local vendors whenever possible and 
practical. Through the port, Dhofari companies can import raw materials and 
export their goods conveniently – and we like to see ourselves as the most 
important facility for the successful development of Salalah Free Trade Zone.<br>
<br>
<b>What are the future expansion plans?</b><br>
For the container terminal, we have been operating at a level around our 
capacity–as a matter of fact, we on several occasions had to turn down shipping 
lines that wanted to call at the port. And that hurts. We have, therefore, 
embarked on an expansion project that will allow us to grow with our current 
customers as well as attract new business to the port. In April, we will open a 
new fifth berth, and berth six is already under construction. While we are 
already dimensioned for some of the world’s largest vessels, we are now taking 
steps to cater to vessels like the ‘Emma Maersk’ with a carrying capacity of 
more than 11,500 TEU and the world’s largest container vessel. Our new berths 
will feature 970 metre continuous and linear berth, 18 metre water depth 
alongside, and 18.5 metre in the approach channel, 10 of the world’s largest 
gantry cranes–six have already been delivered and are being commissioned right 
now and about 2.2 million TEU extra throughput capacity.<br>
<br>
We are also negotiating with the government for further expansion. We hope to 
continue building further facilities with three berths, for 1,200 metre linear 
quay, 15 more gantry cranes and 2.8 million TEU throughput capacity.<br>
<br>
But all this is just hardware, we also need people to be successful. Training 
was always a high priority with the Port of Salalah and we are proud to be 
called upon to help train and develop people in ports in Malaysia, Egypt, 
Jordan, India and Bahrain, to name a few recent examples. We are also proud that 
we have been able to expatriate Omani colleagues to work in other countries 
through APM Terminals, a 30 per cent shareholder in the Port of Salalah. We want 
to keep it that way and are right now finalising the building of a very large 
training centre at the Port that will ensure we remain strong and competitive. 
At the General Cargo Terminal we have recently invested in two new 30-tonne 
capacity mobile cranes, we have expanded our workforce, and we are fine-tuning a 
‘Master Plan’ for the expansion of both the berths and landside installations 
such as warehouses. <br>
<br>
How do you see government policy towards development of Salalah Port?<br>
The business climate in the Sultanate is one of sustained positive growth and 
there is strong public-private cooperation and partnerships. Not least in the 
ports sector. The Port of Salalah is pleased to work with the ministries. We 
have always found a strong partner where the senior leadership is skilled and 
works very hard at making a difference. To prepare for the future, however, it 
is very important that we also strengthen and empower various administrative 
entities.</font><p align="right" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
