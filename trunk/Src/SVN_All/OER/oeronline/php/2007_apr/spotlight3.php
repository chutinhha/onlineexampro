<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Making global footprint</font></b><font face="Verdana" size="1"><br>
<b>The marine and engineering business of Renaissance Services SAOG is rapidly 
expanding globally </b><br>
<br>
<img border="0" src="spot8.jpg" width="250" height="168" align="left" hspace="6" vspace="4">Renaissance Services SAOG’s Marine and Engineering Group (MEG) is on an 
expansion mode with growing activities in its offshore support vessel fleet, 
afloat ship repair business, fabrication and maintenance activities for the oil 
and gas and energy services sectors.<br>
<br>
MEG has one of the top 10 support vessel fleets in the world with a significant 
presence in the Gulf, the Caspian region and South East Asia. In addition, it 
owns significant oil and gas fabrication and ship repair infrastructure and 
assets and major accommodation and life support facilities for oilfields.<br>
<br>
Recently, BUE Caspian, a wholly owned subsidiary of Renaissance Services SAOG 
and part of MEG, announced two-year extensions on its BP contract for up to six 
vessels operating in the Caspian region in Azerbaijan. Under the terms of the 
extension, the six vessels will operate through 2009 for BP’s offshore 
exploration and drilling operations. The contract extensions being for 4 Anchor 
Handlers, Neftegaz 58, Guiltekin Askerova, Om and Nercha, 1 ROV Seismic Vessel 
Tabriz Khalebeylei and 1 Emergency Response and Recovery vessel the Barra. <br>
<br>
BUE Caspian operates under an Alliance agreement with KMNF, a subsidiary of the 
Azeri state oil company, SOCAR. As Roy Donaldson, General Manager, BUE Caspian 
stated, “This contract demonstrates our capabilities in a very competitive 
market where client expectations on quality, health and safety is critical”. 
Based in Baku, Azerbaijan, BUE Caspian operates a wide range of offshore support 
vessels in the Southern Caspian Sea and has the most modern offshore vessel 
fleets in the region.<br>
<br>
Apart from BUE Caspian, the other subsidiary of Renaissance Services SAOG is 
Topaz Energy and Marine Ltd., Dubai, which is one of the leading marine and oil 
and gas fabrication companies in the Middle East with over 30 years experience 
in the region. Having its headquarters in Dubai, UAE, Topaz operates three 
primary business units, namely, Adyard covering oil and gas operations, Nico 
International; the afloat ship-repair operations and Nico Middle East Ltd. and 
BUE Marine; with ship owning operations. Topaz’s clientele base includes the 
leading international oil and gas companies and vessel owners in the world. <br>
<br>
Nico International, another subsidiary of Renaissance Services SAOG, also 
recently won a construction/fabrication contract from FAL Energy valued at 
US$24.126 million at Jebel Ali Oil Storage Terminal, Dubai. <br>
<br>
This marks a major achievement in the business field of oil storage facility 
construction that the company has been focusing on in view of massive expansion 
around the region. The project involves the fabrication and construction of 
eight storage vessels including supply of equipment, electrical/mechanical 
works, civil and structural works, fire protection installation, testing and the 
pre-commissioning of the tanks prior to hand over.<br>
<br>
Nico International is primarily in the business of marine, engineering and heavy 
fabrication. During 2005, it acquired the industrial business from Fujairah 
Marine Services that has enhanced its industrial project management expertise 
and a track record of successfully completing similar projects. Other members of 
the MEG as well as subsidiaries of Renaissance Services SAOG include Adyard Abu 
Dhabi LLC, and BUE Kazakh (Kazakhstan).</font><p align="right" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
