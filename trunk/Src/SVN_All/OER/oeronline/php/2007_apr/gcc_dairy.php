		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><b><font face="Verdana" size="4">It’s all GAS!</font></b><font face="Verdana" size="1"><br>
        <b>Speculation over a new gas cartel came to naught at MEGAS 2007, the 
        premier gas event held in Qatar recently. Kisara Yatiyawela reports</b></font></p>
        <p align="justify"><font face="Verdana" size="1">
        <img border="0" src="gcc.jpg" align="left" hspace="6" vspace="4">For all the speculation, media focus and warrior-like posturing that 
        surrounded the likely formation of an OPEC (Organisation of the 
        Petroleum Exporting Countries)-type gas cartel, the end result was a 
        complete cooling down of the rhetoric! Caught in the crosshairs of this 
        media and industry frenzy was Qatar, as it was the host nation for the 
        recently concluded 12th Annual Middle East Gas Summit (MEGAS 2007), the 
        venue of which was being seen as an ideal location to break the big 
        story–if not announce the formation of the cartel, at least unveil the 
        preliminary plans in this regard.<br>
        <br>
        But first the background: Speculation about the formation of a gas 
        cartel was first fuelled by Russian President Vladimir Putin, who during 
        a visit to Qatar before MEGAS 2007, found the idea of such a grouping to 
        be ‘interesting’. In fact, he also upped the ante by suggesting closer 
        cooperation among leading gas producers. In his interactions with Qatari 
        media, President Putin didn’t shy away from calling for a joint gas 
        strategy and he went even further by promising to send a high profile 
        team of experts to address MEGAS 2007. <br>
        <br>
        His comments fuelled concerns in energy consuming countries, excited 
        the producers and got industry beats racing. But MEGAS 2007 concluded on 
        March 6 with little more than a whimper on the subject. The conference 
        concentrated its energies on dealing with comparatively tame topics, 
        focusing only on the liquefied natural gas (LNG) industry, as against 
        being the birthplace of a gas group that would have had Europe and the 
        United States in a spin. </font></p>
        <p align="justify"><font face="Verdana" size="1"><b>LNG focus<br>
        </b>Sponsored by industry heavyweights likes Qatar Petroleum, Conoco 
        Phillips, Energy City Qatar and Dana Gas, MEGAS 2007 brought together 
        gas exporters who hold 70 per cent of the world’s reserves. In addition 
        to presenting up-to-date information on the global LNG scenario, the two 
        days of presentations and discussions at the conference also featured 
        numerous country focussed sessions, with papers being presented by 
        senior decision-makers from national and international oil companies. 
        The growing role of the Middle East in the global gas market, growing 
        local gas demand and the need for domestic gas grids and interconnection 
        pipelines, gas price issues, their prospects and the link with oil 
        prices, technical, commercial and financial issues related to the LNG 
        chain, the impact of high gas prices on local, regional and global 
        demand and new innovative uses of gas were among the major topics that 
        featured at MEGAS 2007. <br>
        <br>
        In fact, no sooner had MEGAS 2007 concluded, industry observers 
        shifted their focus to FLAME 2007, Europe’s largest, premier and most 
        comprehensive gas event. Held in Amsterdam from March 12-16, FLAME 2007 
        did debate the issue of a gas cartel, reaching the conclusion that it 
        was short-lived idea. In fact, the emerging consensus at FLAME 2007 was 
        that the gas producing nations were far from likely to form a pricing 
        cartel similar to OPEC in the next five years. As a poll of delegates, 
        who were largely from Europe and the US, taking part in FLAME revealed, 
        65 per cent did not expect a gas OPEC to appear any time soon, a belief 
        shared by a speaker from the US Federal Energy Regulatory Commission (FERC). 
        “There certainly is a potential, but in the US we don’t believe it is 
        likely to happen quickly and certainly not in the next five years,” said 
        FERC energy commissioner Suedeen Kelly. <br>
        <br>
        The delegates based their reasoning on the fact that today natural gas 
        was not a commodity like oil around which a cartel could be built. This 
        is because most natural gas is today transported through pipelines and 
        sold on a regional basis via long-term contracts, with price being set 
        for periods as long as 20 years. Against this, oil is transported by 
        tankers as well as by pipelines, and can be redirected from one buyer to 
        another in response to market demand. As a result, oil is traded 
        globally and is subject to daily price fluctuations. Said Christian Egenhofer, an analyst with the Center for European Policy Studies in 
        Brussels, “I don’t think it (formation of a gas cartel) is a terribly 
        realistic option.”<br>
        <br>
        <b>Russia’s strategy<br>
        </b>As for President Putin’s posturing, Egenhofer said this could be just 
        Russia’s ham-handed way of making European countries think twice about 
        reducing their reliance on Russia as a source of energy, particularly 
        since Europe gets about a quarter of its natural gas from Russia. “It’s 
        just part of a broader Russian strategy to raise the stakes and say to 
        Europe–You’re dependent on us.”<br>
        <br>
        Yet, industry experts say the seed has been sown and it will take some 
        time for the idea to grow and bear fruit. According to them, a gas group 
        could become not just useful but also essential once LNG comes to occupy 
        its rightful place in the global energy marketplace. Like oil, LNG can 
        be shipped by tankers and therefore traded in spot markets where prices 
        fluctuate. “There are reasons why a gas cartel isn’t realistic right 
        now,” said Claudia Kemfert, an analyst with the German Institute for 
        Economic Research in Berlin. “But decisions made now about the gas 
        market are very important because LNG is a long-term investment, with 
        ramifications 15 years from now. With that in mind, the idea of a cartel 
        becomes (just like Putin said) more interesting.”<br>
        <br>
        <img border="0" src="gcc1.jpg" width="170" height="266" align="right" hspace="6" vspace="4">Interestingly, 41 per cent of the delegates at FLAME 2007 said that 
        Russian and Caspian Sea producers were likely to be the most reliable 
        suppliers of LNG in the future. They were followed by North African 
        suppliers (nearly 28 per cent votes) and then by Middle Eastern 
        producers (22 per cent).<br>
        <br>
        An unfazed Qatar, however, sees its share of the world’s natural gas 
        energy market rising to 25 per cent by the year 2025. With the world’s 
        third largest natural gas reserve located in Qatar, the country has firm 
        plans to become the leading LNG exporter by the year 2012. Qatar’s 
        Second Deputy Premier and Minister of Energy and Industry Abdullah bin Hamad Al Attiyah recently said that energy experts predict a 2.4 per 
        cent growth rate for the domestic gas industry over the next 20 years. 
        This will take the current consumption figure from 285 billion standard 
        cubic feet a day to 440 billion standard cubic feet. <br>
        <br>
        Providing a glimpse into Qatar’s plans for the LNG sector, minister 
        Abdullah bin Hamad Al Attiyah said that the annual LNG export capacity 
        to the Far East, the US and Europe is set to go up to 77million tonnes 
        by 2010, far exceeding the current figure of 30 million tonnes. Citing 
        some of the challenges faced by Qatar, he spoke of the lack of 
        facilities Oman constantly encounters for unloading LNG in many parts of 
        the world. In order to circumvent this, Qatar has strategically involved 
        itself in building the required LNG receiving terminals in chosen 
        locations in both Europe and the US. Large LNG carriers and plants have 
        already been built by Qatar and the country is seeking to construct six 
        more LNG trains. Currently, Qatar operates the largest LNG trains in the 
        world, with an annual capacity of 4.7 million tonnes. <br>
        <br>
        Speaking about proposed medium and long-term perspective for new LNG 
        ventures in Qatar, Abdullah bin Hamad Al Attiyah said: “Qatar is 
        focusing on developing its existing supply chains while raising the 
        level of technological innovations used in the LNG industry. The country 
        is committed to adopting sophisticated technologies and increasing its 
        investment in some countries for building gas infrastructure.” According 
        to him, Qatar was also keen to maximise the usage of traditional 
        pipelines for supplying gas in a manner similar to what the country has 
        been doing locally (Qatar fuels its power and industrial plants using 
        piped gas).</font></p><hr width="90%" size="1">
