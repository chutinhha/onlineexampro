<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Widening the Horizon<br>
</font></b>
<font face="Verdana" size="1"><b>Bahrain moves to benefit from its FTA with the US, both directly and with other 
countries<br>
</b>
<br>
<img border="0" src="closeup.jpg" width="145" height="281" align="right" hspace="6" vspace="4">The US-Bahrain Free Trade Agreement (FTA) is now on in full swing. Bahrain is 
making every possible effort to get the most out of the accord that went into 
effect in August 2006. <br>
<br>
In March, Bahrain’s crown prince Sheikh Salman bin Hamad al-Khalifa led a 
delegation of executives and officials to a series of business meetings in the 
city of Chicago. This marked the first time that the Crown Prince had personally 
presided over a visit to the US dedicated to promoting the FTA deal. Later, the 
delegation continued the second part of its journey by visiting Houston in the 
state of Texas for the same purpose.<br>
<br>
To be sure, Bahrain’s interest in the FTA with the US does not come from a 
vacuum. Rather, the authorities in Manama appreciate potential benefits of the 
deal with respect to Bahrain’s economy, notably job prospects. The US-Bahrain 
FTA removes trade barriers related to goods and services and encourages 
investments between the two countries. <br>
<br>
<b>Advantages to Bahrain’s economy<br>
</b>The FTA provides numerous benefits to Bahrain including getting access to the 
world’s largest GDP and import market. Other benefits include enticing 
investments from American and other enterprises, besides encouraging authorities 
in Bahrain to streamline certain labour and business laws. <br>
<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Access to the largest economy</b>: The American economy is by far the largest in 
the world and accordingly can accommodate any increase in exports from Bahrain. 
American gross domestic product stands at US $ 13,000 billion. In contrast, 
Bahrain’s GDP amounts to less than 1 per cent of the US’ GDP. <br>
<br>
According to the US Census Bureau, American imports of goods and services 
amounted to US $ 2,201 billion in 2006, registering a 10.5 per cent growth in 
comparison to 2005. Also, total exports of goods and services from the US stood 
at US $ 1,439 billion, up 12.8 per cent from 2005. Thus, the US trade deficit 
amounted to US $ 764 billion, undoubtedly the highest in the world. <br>
<br>
According to statistics published by the US Census Bureau, US-Bahrain bilateral 
trade amounted to US $ 1,123 million in 2006, with Bahrain maintaining a surplus 
of nearly US $ 142 million. In 2005, bilateral trade had amounted to US $ 783 
million with Bahrain enjoying a smaller surplus of US $ 81 million. Clearly, the 
FTA has allowed Bahrain further unrestricted access to the US market. Bahrain’s 
goods exports to the US comprise apparel and clothing accessories, aluminium, 
fertilisers, organic chemicals, mineral fuels and oils, plastics, and electrical 
machinery. Conversely, US exports to Bahrain focus on aircraft, machinery, 
vehicles, pharmaceutical products, toys, games and sports equipment apart from 
agricultural exports like poultry, snack foods, cotton, processed fruit and 
vegetables. </font>
