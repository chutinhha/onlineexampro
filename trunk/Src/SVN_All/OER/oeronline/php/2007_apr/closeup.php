<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Streamlining laws to entice American businesses</b>: The FTA is expected to 
increase American investments in Bahrain. According to the United Nations 
Conference on Trade and Development (UNCTAD), worldwide outward foreign direct 
investment (FDI) stood at US $ 916 billion in 2005. The US contributes some 30 
per cent of global outward FDI. <br>
<br>
Aware of the potential benefits, Bahrain authorities showed a readiness to amend 
laws deemed essential to attract possible American investments. In fact, the 
American side had insisted that the FTA could not come in force unless the 
Bahraini side was prepared to amend legislations extending protection to 
intellectual property rights (IPR), together with specific penalties against 
violators. Bahrain now hopes that some US firms would use Manama as a gateway 
for doing business in the GCC region and beyond.<br>
&nbsp;<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Opening up prospects</b>: Booz Allen Hamilton, an American consulting firm, 
believes that the FTA opens up opportunities for six sectors of Bahrain’s 
economy. These are: <br>
<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Attracting investments</b>: There is also likely to be a spillover effect with 
Bahrain enticing FDI and other forms of investments from other countries. The 
reasoning cited is that the amended laws and improved business environment would 
assist the authorities in marketing Bahrain worldwide. According to UNCTAD, 
inward FDI to Bahrain amounted to US $ 1,049 million in 2005, up from US $ 865 
million in 2004.<br>
<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Upgrading 
labour laws</b>: Bahrain agreed to update all labour laws in compliance 
with International Labour Organisation (ILO) standards. For example, Bahrain 
officials are required to ensure that school-age children are barred from 
working. Also, the Bahraini government has taken up responsibility to verify 
that labour laws do not discriminate against foreign workers in any way. <br>
<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Liberalising business regulations</b>: The FTA has purportedly convinced Bahrain 
authorities to streamline the business environment at large. These measures 
include reducing charge for new or renewal of commercial registration to BD 20 
per annum for most business activities (excluding financial services). More 
recently, the authorities made two extraordinary decisions, serving the needs of 
consumers and companies. One such law requires firms to post prices of products 
in the most visible way. The law applies on all retail outlets including small 
shops selling vegetables. The other provides commercial enterprises the right to 
engage in sales activity at anytime without seeking any permission. <br>
<br>
<img border="0" src="close1.jpg" width="13" height="11" align="absbottom"><b>Improving chances of EU-GCC FTA</b>: The argument goes that the US-Bahrain FTA 
(together with subsequent deal with Oman) played a part in convincing the 
European Union to clinch a deal with the GCC. In retrospect, the EU and GCC 
signed a framework for entering into talks on establishing a free trade area in 
1988. However, the European side kept pressing GCC states to comply with new 
demands, including environmental protection. However, the deal could be signed 
later this year if the GCC states agree to EU request of scrapping cap on 
foreign ownership of business. At the moment, the GCC has in place a 49-per cent 
foreign ownership of firms. <br>
<br>
<b>Lengthy process</b><br>
In fact, Bahrain had to go a long way before meeting US requirements for 
implementing the deal. In retrospect, formal FTA negotiations started in Bahrain 
in January 2004. Representatives of the two countries succeeded in working out 
details of the agreement in only five months. The deal was formally signed in 
Washington in September. <br>
<br>
Yet, legal experts spent several months translating the text from English into 
Arabic. For its part, Bahrain completed endorsement process of the deal in July 
2005. Still, the US Congress only endorsed the deal in December. Nevertheless, 
the FTA deal proved popular in the House of Representatives and got an 
overwhelming majority (327 yes to 95 no votes). It is believed that the Congress 
supported the FTA with Bahrain partly because it posed no threat to American 
farm industry.<br>
<br>
President George W. Bush signed the agreement into law in January 2006. However, 
implementation was delayed until August till Bahrain satisfied American 
requirement on laws governing intellectual property rights. <br>
The US-Bahrain FTA went into effect in August 2006, nearly two years after the 
clinching of the deal. But, it marked the first time that the US had implemented 
such an accord with any GCC state. Oman became only the second GCC country with 
which the US has signed a separate FTA.<br>
<br>
In short, the FTA provides prospects for Bahrain to address economic challenges, 
notably maintaining some occupations and creating new job opportunities stemming 
from prospects of increasing exports to the US market. Unemployment, which 
stands at more than 10 per cent, is the main economic challenge facing Bahrain.
<br>
<br>
