</font><font face="Verdana" size="2" color="#CC3333"><b>Castle Bidco agrees to US $1.4-b Crest takeover</b></font><font face="Verdana" size="1"><br>
<br>
A consortium of UK bank HBOS and property magnate Tom Hunter has said it had 
agreed to a takeover of Crest Nicholson, valuing the UK housebuilder at around 
£715 million (US $ 1.4 billion). Castle Bidco said it would pay 620 pence per 
share in cash for each Crest share and a final dividend of 9.7 pence per share 
would be paid by Crest. Castle Bidco said the offer represented a premium of 
10.2 per cent to 571.5 pence per Crest Nicholson share, the closing price on 
November 9, the day before Crest said it had received an approach. Crest said 
talks with the consortium were continuing, as the deadline for a takeover bid 
was pushed to early March and reiterated it would recommend a 620 pence per 
share bid, but there could be no certainty an offer would be made. In January, 
Crest said Castle Bidco had been considering making a £700-million cash offer at 
620 pence per share, along with the right to the final dividend of 9.7 pence a 
share. </font><hr color="#C0C0C0" width="90%" size="1">
