</font><font face="Verdana" size="2" color="#CC3333"><b>Boeing boss sees threat from China</b></font><font face="Verdana" size="1"><br>
<br>
China will soon be threatening major Western aircraft makers, Boeing boss James 
McNerney said in an interview to German weekly Der Spiegel. Warning that it was 
wrong to think that there would only ever be Boeing and Airbus in competition, 
McNerney was quoted as saying, “The Chinese have well-trained people, a strong 
spirit of enterprise and an enormous domestic market.” McNerney said the US 
manufacturer had learned its lesson after failing to heed the threat from its 
European rival. “We were market leaders and we went to sleep, instead of staying 
wide awake,” he added. Asked about Airbus’s current problems, stemming in part 
from difficulties with its giant A380 airliner, McNerney said the two companies 
had different views of where the market lay. “We believe the future is with 
smaller aircraft capable of flying long distances between medium-sized cities, 
rather than in the development of large hubs” favoured by Airbus, he said. But 
McNerney added that the A380 was “a marvellous airplane, very exciting from a 
technological view.” </font><hr color="#C0C0C0" width="90%" size="1">
