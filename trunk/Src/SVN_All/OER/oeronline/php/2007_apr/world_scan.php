</font><font face="Verdana" size="2" color="#CC3333"><b>China likely to allow foreign companies’ yuan 
bonds in 2007<br>
</b></font>
<font face="Verdana" size="1"><br>
China is likely to allow foreign companies to issue yuan-denominated bonds on 
domestic markets this year. Citing a top central bank official. Wu Xiaoling, the 
vice-governor of the People’s Bank of China, the China Securities Journal 
reported that the central bank was in favour of foreign companies and 
international organisations issuing yuan-denominated bonds in China. Supervisory 
authorities would not reject applications from institutions registered in China 
as long as the companies were engaging in legitimate business operations, Wu 
said. Foreign companies and international organisations currently must transfer 
foreign exchange into China from abroad when they need capital for domestic 
investment purposes. The World Bank’s International Finance Corp and the Asian 
Development Bank are currently the only foreign entities allowed to issue 
yuan-denominated bonds in China. <br>
