</font><font face="Verdana" size="2" color="#CC3333"><b>Work on tripartite gas pipeline to start in ’09</b></font><font face="Verdana" size="1"><br>
<br>
Iran, Pakistan and India are considering a plan to begin construction of the US 
$ 7.23-billion gas pipeline by September 2009 on a segmented basis to ward off 
possible US-laws aimed at punishing oil companies that deal with Iran. “Each 
country will build the pipeline in the respective territories and in fact Iran 
has already started laying pipeline from Assaluyeh to Iran Shehr to serve its 
Eastern parts that would be extended about 200 kilometres to Pakistan border,” a 
senior official in the Pakistan’s petroleum ministry has said. Distance between 
Iran Shehr to the Pakistan border is about 200 km. He said a tentative schedule 
for the implementation of the pipeline has also been agreed upon envisaging 
completion of the project between June-September in 1914. However, he explained 
that Pakistan will construct the 655 km pipeline in its territory on the build, 
own, operate and transfer (BOOT) basis. For this, the government will appoint 
lead sponsors immediately after signing of the gas sales and purchase agreement 
with the National Iranian Gas Company (NIGC) by June 2007.</font><hr color="#C0C0C0" width="90%" size="1">
