</font><font face="Verdana" size="2" color="#CC3333"><b>US has Green Card, EU to have Blue Card</b></font><font face="Verdana" size="1"><br>
<br>
Inspired by the famous American “green card” scheme, the European Commission is 
considering introducing its own “blue card” system to attract more qualified 
immigrants. An impact assessment is currently being drawn up and will be ready 
“in the next two months or so” after which the Commission will draft detailed 
proposals, EU justice spokesman Friso Ascam Abbing has said. He admitted that EU 
member-states hold differing opinions over legal migration but added that 
“everyone understands immigration will be part of the solution to the aging 
population. “We had better manage immigration properly as it is going to happen 
anyway,” he said. The “blue card”–taking its name from the EU flag, blue with 
gold stars–”would allow holders to move around the EU,” EU Immigration 
Commissioner Franco Frattini said. “Today if a multinational wants to move an 
engineer from a non-EU country from Rome to Brussels, they have to go through 
visa procedures, get a residency permit, a work permit,” explained Frattini, who 
plans to present a formal proposal on the matter in September. </font><hr color="#C0C0C0" width="90%" size="1">
