<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4"><b>Do Not be Ruled...<br>
by Greed</b></font><font face="Verdana" size="1"><br>
<b>It is time to reduce high-risk exposure to emerging markets and instead buy 
undervalued blue chips in the GCC, advises Matein Khalid<br>
</b>
<br>
<img border="0" src="periscope.jpg" width="137" height="337" align="right" hspace="6" vspace="4">When global markets turn nasty, there are few places that one can hide in. The 
crash in Shanghai triggered a succession of interconnected events in the world 
financial markets. A massive unwinding of the carry trade, rise in the Japanese 
Yen and Swiss franc against the dollar, meltdown in the US’ subprime mortgage 
market with 20 lenders going out of business, a plunge in emerging stock markets 
indices from Mumbai to Sao Paulo to Istanbul, a 500-point weekly drop in the Dow 
Jones–the worst since 9/11–and the end of the NASDAQ Silicon Valley tech bubble, 
falls in European equity indices and a drop in the price of gold. Volatility 
surged and the Chicago VIX index, barometer of the stock market fear and greed, 
almost doubled in a week. Fund managers cut their exposure to emerging markets 
as retail outflows accelerated in the world’s hottest asset class of the 
millennium.<br>
<br>
Sure, normalcy has returned to the financial markets after a robust US February 
jobs data, scaling down of US recession forecast by Dr. Alan Greenspan and the 
recovery of the dollar to 118 Japanese yen. Also, the R–word nightmare is an 
unlikely scenario for emerging markets investors as gold prices are still at 
US$650, crude oil is above US$60 an ounce and the IMF has not called for a 
global slowdown. However, I doubt if we are totally out of the woods yet. EM 
fund cash levels are still dangerously low. Risk still haunts the global capital 
markets, with the VIX at 14, swap and credit default spreads still higher, Wall 
Street investment banking shares nearer their lows than recent highs and an 
eerie calm in Shanghai /Hang Seng. Moreover, global interest rates are set to 
rise as the ECB and Bank of Japan tighten credit and the Peoples Bank of China 
implements Beijing’s order to cool stock and property speculation. It is obvious 
that the easy money policies of the past, nirvana for the emerging markets, are 
now over.<br>
<br>
This does not mean an EM bear market lies ahead (unless the US attacks Iran). 
Yet, investors should brace themselves for volatile trading, the possibility of 
Mexican /Russian /Thai type EM contagion witnessed in the 1990s and occasional 
market panics. It is time to reduce exposure in high risk, high beta, high 
valuation emerging markets such as India and China. It is time to seek value in 
stock exchanges where fear, and not greed, rules the roost, where investor 
sentiment is bearish, where companies still deliver profit and where correlation 
coefficients are not tied to Wall Street or China. It is time to buy undervalued 
blue chips in the GCC.<br>
<br>
GCC equities proved their value as a non–correlated assets class to global 
equities, emerging markets and the US economic cycle. The trauma of the global 
markets did nothing to change the stealth bull market in the Saudi Tadawul index 
(up 20 per cent from its lows) or in selected GCC blue chips, There are clear 
signs that the smart money, primarily offshore fund managers, have begun 
accumulating liquid, large cap GCC shares. Investor sentiment in the GCC stock 
markets also swung between excessive greed and excessive fear in 2006. <br>
<br>
The outperformance of the Saudi Tadawul index, the generator of contagion across 
the GCC bourses in the bear market lows last summer, shows risk metrics have 
fallen in the GCC since January. I am particularly encouraged that the GCC 
markets have moved into a cautious bullish mode despite bad geopolitical news 
from Iran / Iraq, a rise in Saudi interest rates, some major corporate profit 
falls, softening oil prices, glut in hot property markets, deceleration in GDP 
growth and a liquidity squeeze by regional central banks on leveraged stock 
speculation.<br>
<br>
After the global market plunge, the earnings/interest rates matrix demonstrates 
that the GCC stocks markets, with the exception of Saudi Arabia, are 
attractively priced compared to major emerging markets such as India and China. 
Ironically, when India and China were cheap in 2002–03, investors in GCC had 
zero interest in emerging Asian equities. However, now that Shanghai and Mumbai 
command the highest valuation multiples in the emerging markets at a time when 
the PBOC and RBI, respectively, are raising interest rates to combat inflation, 
GCC banks and family office money is flooding the two mega Asian markets.<br>
<br>
There are sound reasons to be bullish on GCC equities. Valuations relative to 
interest rates and profit growth are no longer excessive, particularly since the 
Federal Reserve’s next interest rate move is likely to be a cut. Unlike in the 
bubble era, stock markets now differentiate between quality, quantity and 
sustainability of earnings. <br>
<br>
Speculative companies can no longer rely on creative accounting and leveraged 
equity speculation to boost share prices. The shocking earnings’ miss by so many 
GCC banks, brokers, insurance companies and even cement plants proves that the 
mirage of paper profit no longer cuts any ice. Moreover, regulators are no 
longer willing to tolerate corporate or broker violations. Above all, the Gulf 
IPO mania has receded even though regulations are still archaic and too many 
start-ups are still being financed. As long as oil prices remain above US $ 50, 
liquidity will remain plentiful in the Gulf, even though bank credit growth may 
slow down. These are all ingredients of an emerging bull market in the GCC.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
