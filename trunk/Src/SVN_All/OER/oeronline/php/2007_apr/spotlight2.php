<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Cruising ahead</font></b><font face="Verdana" size="1"><br>
<b>Oman’s expanding shipping industry has set the country’s economic flywheel 
spinning with global shipping lines increasingly marking their presence in the 
Sultanate, reports Sunil Kumar Singh</b><br>
<br>
<img border="0" src="spot4.jpg" align="left" hspace="6" vspace="4">A 
booming economy triggers a multiplier effect on different sectors. And, the 
shipping sector is no exception. In the case of Oman too, the growing base of 
manufacturing activities and port infrastructure, soaring investments in 
petrochemical industries, surge in real estate and tourism, has had its effect 
on sea cargo movement and related services. Little wonder, the shipping 
industry, as a whole, is growing at a fast pace to cater to the burgeoning 
imports of project cargo and raw material and finished products exports, among 
others.<br>
<br>
<b>Value addition</b><br>
Shipping companies and agencies operating in Oman have come to occupy a 
prominent place in harnessing the country’s core strengths in LNG, crude oil 
transport, refined oil products and emerging petrochemical businesses. As the 
table shows, the total cargo handled at Sultan Qaboos Port from January 2006 to 
January 2007 recorded a whopping 57.4 per cent year-on-year (YoY) growth. 
Similarly, Salalah Port too posted a 59.8 per cent YoY growth in total cargo 
handled during the same period. One notices a similar increase if the number of 
mooring vessels is also considered. As per data available with the Ministry of 
National Economy, in Mina Al Fahal Port, which specialises in oil exports, the 
volume of unloaded petroleum products cargo increased from 211,000 tonnes in 
2003 to 417,000 tonnes in 2005, while the volume of loaded petroleum decreased 
marginally from 39,079,000 tonnes to 36,759,000 tonnes in the same period. 
However, the number of vessels entering the port increased from 356 in 2003 to 
367 in 2005. <br>
<br>
<img border="0" src="spot5.jpg" width="250" height="174" align="right" hspace="6" vspace="4">In fact, with the increasing industrialisation in the boomtowns of Sohar and 
Salalah as also the rapid development of residential hubs across the Sultanate, 
there has been a corresponding increase not only in freight movement but also in 
port calls, project cargo calls, refined product exports, dredging activities, 
logistics, warehousing, freight forwarding and other related services. As Patrik 
Hallden, GM, Gulf Agency Company, Oman, (GAC Oman), states: “Logistics 
activities are growing in Oman and we foresee that this sector has tremendous 
potential for a majority of import as well as export sectors.” Adds Vinoj 
Tharian, branch manager, Clarion Shipping Services, Muscat, “for a couple of 
years, we’ve been posting 100 per cent increase in profits owing to rapid 
demands of logistics and fright forwarding services in Oman.” The ship repair 
business is another sector with tremendous growth potential in the Sultanate, 
and the upcoming dry-dock facilities in the Al-Duqm Port is expected to be a 
major shot in the arm for Oman’s shipping industry. Industry experts say the GCC 
region has inadequate number of dry docks compared to other parts of the world. 
Therefore, the facility at Duqm Port is expected to attract a considerable 
number of customers, both from the region as well as globally.<br>
<br>
<b>Major players</b><br>
Along with the burgeoning cargo carrying capacity of the maritime industry, Oman 
is also increasingly becoming home to world-class shipping lines and container 
terminal operators. At the heart of the shipping industry in the Sultanate is 
Oman Shipping Company (OSC). Set up in 2003 and wholly owned by the government 
through the Ministry of Finance and Oman Oil Company to develop maritime 
transportation industry in Oman, OSC is involved in exporting Omani LNG products 
and in the LNG value chain. It has two subsidiaries – Oman Ship Management 
Company, which looks after ship management operations, and Oman Charter Company, 
which is responsible for chartering OSC’s fleet. The company owns and operates 
six LNG vessels, having a total capacity of 11.8 million cubic metres. It 
transports LNG from Oman to the international market, mainly the Far East and 
Europe. In addition, it owns a product tanker and one chemical tanker. OSC has 
recently invested in a very large crude carrier (VLCC), a very large gas carrier 
(VLGC) and two product tankers. It is today seeking to expand and diversify its 
fleet, and gain expertise in areas like the VLCC, product tanker, petrochemical 
carrier, bulk carrier, LPG tanker, methanol tanker and container markets.<br>
<br>
Another leading player is the Barwil Unitor Ships Service (BUSS), a joint 
venture between Wilhelsmsen Maritime Services and the WJ Towell Group of Oman. 
It services various types of vessels ranging from general cargo vessels to 
tankers, and cruise ships to fishing vessels and barges. BUSS is part of the 
world’s leading maritime services network, with the capability to serve 2,200 
ports in 116 countries. Management sources said last year BUSS Oman handled over 
600 port agency calls in Muscat, Sohar, Sur, Khasab and Salalah.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<img border="0" src="spot7.jpg" width="175" height="242" align="right" hspace="6" vspace="4">Similarly, Inchcape Shipping Services (ISS) offers a slew of services ranging 
from port agency to airline cargo agencies and a packing and removals business. 
Though its core business remains port agency, ISS has also been diversifying 
into cargo and defence services. As Jim Robb, General Manager, ISS says, “During 
2006, we saw exciting growth in the booming Sohar market. We have seen 
significant new businesses on the marine and cargo services front and expect 
this market to blossom over the next five years. There has been a boost in port 
calls and activity in the last quarter of 2006 and a number of new petrochemical 
and port-related projects are either under construction or are being planned.” GAC Oman is also one of the largest companies operating in the shipping and 
logistics sectors in Oman. Part of the international network of GAC’s shipping, 
logistics and marine market, GAC Oman operates from all ports, with offices in 
Muscat, Sohar, Salalah, Khasab and Sur, handling all types of vessels ranging 
from large tanker vessels to luxury cruise liners. Patrik Hallden, GM, GAC Oman, 
explains, “Oman is enjoying a strong growth and this has had a positive impact 
on us. Last year was the best one ever in our company’s history and due to the 
strong wind in the sails, we are confident that we will continue on this path of 
success.” <br>
<br>
South Korea-based Hanjin Shipping Company Ltd is another major shipping line 
operating in the Sultanate’s waters. Modern Shipping Services, its agency in 
Oman, posted 14 per cent growth last year, with its annual cargo carrying 
capacity averaging 4,000 TEUs of imports and around 3,000 TEUs of exports since 
2003. Having a global footprint, Modern Shipping Services’ operations are 
connected to the Sultanate via Dubai through seven services lanes.<br>
Then there is Khimji Ramdas Shipping Division, which has three shipping agency 
licences offering line-based agency services, customs clearance, transportation, 
consolidation, air cargo GSA, freight forwarding, insurance, ship-handling, 
bunkering, international agency representation, etc.<br>
<br>
And last, there is Maersk Shipping Services that has been working as the agent 
for A.P. Moller-Maersk-owned brands — Maersk Line and Maersk Logistics — in Oman 
since 1999. Maersk Line, Salalah Port’s biggest customer, made 950 mother vessel 
calls at the port and was a major contributor in the port’s total throughput of 
2.4 million TEUs in 2006. Moreover, APM Terminals, a division of A.P. 
Moller-Maersk and a partner in Salalah Port is also involved in the port’s 
management, undertaking transhipment operations, ship-handling and vessel 
husbanding activities. Maersk Line is also involved in Sohar Port, where it 
commenced a weekly service in November last year. At Sultan Qaboos Port too, 
Maersk Line’s feeder vessels call in for export and import cargo handling. Apart 
from shipping services, A.P. Moller-Maersk in also involved in logistics and 
transportation service in Oman. The bottomline is that the maritime industry in 
Oman is on the high growth path. And, if industry analyses are any indication, a 
boom is likely in the Gulf region’s shipping industry over the next decade with 
the shipment activities in the region expected to multiply three-fold by 2020.</font></p>
