        Mazda6 was chosen over any other, was its comfort, quality driving 
        dynamics, high-end craftsmanship and excellent road holding and 
        cornering ability. “When it comes to choosing a fleet car very few cars 
        can compete with the Mazda6 on parameters like design, safety, 
        boot-space, ride quality, running cost, maintenance cost and residual 
        values. A lot of competitive car brands in this segment offer a lot but 
        very few can offer all this in one package,” says Formose Pereira, 
        Business Development Manager.<br>
