        </font><b><font face="Verdana" size="2" color="#CC3300">A sporty concept from Chrysler 
        </font></b><font face="Verdana" size="1"><br>
        <br>
        <img border="0" src="carnews2.jpg" align="right" hspace="6" vspace="4">Designed to vaguely resemble classic British sports cars, the 2007 
        Chrysler Nassau concept car claims to do 0-60 in 5.1 seconds, making it 
        phenomenally quick for a car with a 120-inch wheelbase. The Chrysler 
        Nassau concept is, according to Alan Barrington, principal exterior 
        designer, “a more emotional and artistic articulation of what it means 
        to be a Chrysler. Traditional exterior proportions have been enhanced 
        with a silhouette that recalls the classic English ‘shooting brake. This 
        provides SUV-like interior volume with a lower, more roadworthy 
        physique. In order to attain a more compact appearance, the Nassau has 
        deliberately concise front and rear overhangs, with the body surface 
        wrapped around the large 10-spoke 21-inch wheels.” He added that the 
        Chrysler Nassau concept was created to form the effect of a classic 
        sculpture–an artistic approach with a shapely flowing of lines that give 
        the impression of movement even while standing still. The Chrysler 
        Nassau concept’s profile line draws down and into the tail lamp, leading 
        the eye of the observer toward to the dramatic back end which creates 
        its unexpected ‘shooting brake’ appearance. <br>
