		<font face="Verdana" size="1"><br>
        </font><b><font face="Verdana" size="2" color="#CC3300">A chapter in excellence 
        </font></b><font face="Verdana" size="1"><br>
        <br>
        Bridgestone Corporation has earned a name for itself as one of the 
        world’s leading tyre companies that produces a wide range of tyres using 
        the most sophisticated technologies, the most popular being its Run Flat 
        Tyres (RFT). <br>
        <br>
        Bridgestone RFT comes into immediate effect in the instance of pressure 
        loss, keeping the vehicle under control and avoiding potentially fatal 
        situations. Bridgestone RFT enables passengers to continue driving in 
        comfort and safety for a certain distance at a specified speed after a 
        puncture. RFT are increasingly being fitted on premium cars. RFT removes 
        the need to carry a spare tyre, reducing weight and offering the end 
        user more storage space and the car manufacturer more design 
        opportunities. Incidentally, Bridgestone Motorsport is the official tyre 
        supplier to FIA Formula 1 World Championship for the present year 2007 
        as well as for 2008, 2009 and 2010. The decision was recently announced 
        by the sport’s governing body, the Federation Internationale Automobile 
        (FIA). <br>
