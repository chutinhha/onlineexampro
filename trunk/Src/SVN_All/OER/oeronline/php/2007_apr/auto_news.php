</font><b><font face="Verdana" size="2" color="#CC3300">Safety first: Nissan Pathfinder</font></b><font face="Verdana" size="1"><br>
<br>
        <img border="0" src="carnews3.jpg" width="200" height="126" align="left" hspace="6" vspace="4">The Nissan Pathfinder has earned Euro NCAP’s (European New Car 
        Assessment Program) highest award–a 5-star safety rating for side-impact 
        (front and rear seat) crash-test safety. A recently released study by 
        Euro NCAP ranks the new Nissan Pathfinder as one of the safest SUV 
        vehicles in the market for adult occupant protection. The Pathfinder 
        scored well in all three categories tested, passenger safety, child 
        safety and pedestrian safety, ensuring that it won the rank of being one 
        of the safest SUV vehicles available. The Pathfinder scored 32 points, 
        while the average score for the same vehicle category is 28 points. The 
        Nissan Pathfinder incorporates the Nissan Safety Shield approach to 
        safety. It includes rigorous crash testing, over 50 standard safety 
        features on every vehicle, available advanced safety technologies, and 
        child safety to help protect both drivers and passengers. <br>
