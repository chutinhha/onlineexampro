<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">OOC and MOL:Mutual Benefits</font></b><font face="Verdana" size="1"><br>
<b>While the two firms appear to be geographically odd bedfellows, MOL’s 
refining, distribution and manufacturing strengths are expected to dovetail with 
OOC’s business ties in the Middle East and Central Asia, as well as its 
technical know-how<br>
</b><br>
<img border="0" src="economy.jpg" align="left" hspace="4" vspace="2" width="155" height="356">Oman Oil Corporation’s recent purchase of a sizeable stake in MOL, the Hungarian 
energy company, came as something of a surprise, but could work out to be good 
business for both parties. OOC’s move secures it an interest in a key player in 
the Central European downstream oil market that should help the Sultanate’s oil 
sector diversify its investments. This is particularly important for a country 
in which oil extraction is difficult and costly.<br>
<br>
From MOL’s side, the deal has been widely publicised as a move to block hostile 
takeover bids by other firms. But the Hungarian company may also be looking to 
reap the benefits of taking part in some OOC projects, which the structure of 
the “sale” may well permit. The deal will see OOC take 8.8 million shares in 
MOL, or around 8 per cent, for approximately US$1.28 billion. This values each 
MOL share at US$145.43, 10 per cent more than their market value on March 7, the 
last day of trading before the sale was made public. According to reports, MOL 
will also receive minority shares in OOC assets in exchange for the stake.<br>
<br>
<b>Complementary strengths</b><br>
While the two firms appear to be geographically odd bedfellows, MOL’s refining, 
distribution and manufacturing strengths are expected to dovetail with OOC’s 
business ties in the Middle East and Central Asia as well as its technical 
know-how.<br>
From OOC’s point of view, an investment in MOL offers the advantage of getting a 
stake in a relatively large company (indeed, Hungary’s biggest), which operates 
in many of the fast-growing markets in Central and Eastern Europe (CEE). OOC 
will have a share of profits from MOL’s activities in the downstream sector and 
retailing, an area in which it is particularly strong. Perhaps, more 
importantly, by buying into a firm which operates large refineries, has 
significant petrochemical interests and operates a large number of petrol 
stations in CEE, OOC is diversifying its portfolio, both geographically and in 
terms of the parts of the oil extraction-to-sale chain it has interests in. OOC 
may also be looking to harness MOL’s refining capacities within the Sultanate 
itself.<br>
<br>
This diversification is particularly important for Oman’s oil industry. 
Extraction costs in the Sultanate are high and currently proven reserves are low 
by regional standards. The high global price of oil is currently bringing in 
tidy profits, making even Oman’s extraction expenses appear paltry, but the 
margins may tighten considerably again in the medium to the long term. Therefore 
through this investment, OOC is fulfilling its aim of investing today’s healthy 
oil windfalls for an uncertain tomorrow. MOL’s presence in several parts of the 
oil chain in strong, stable markets makes it a good bet for OOC.<br>
<br>
Furthermore, MOL’s expertise and large holdings in refining in Europe may well 
be of specific use to Oman. The Sultanate is making efforts to establish itself 
as a refining and re-export centre for the region, as well as building up its 
petrochemicals sector. Even if the oil is not sourced here, the thinking goes, 
Oman can refine it. Here, the location of its ports outside the vulnerable 
Straits of Hormuz gives it an advantage as an export centre.<br>
<br>
In a sense, OOC is acting in a manner similar to the Gulf sovereign wealth funds 
(SWFs), which have recently been shelling out vast sums to purchase chunks of 
enterprises abroad, particularly in North American and European banks, who are 
paying a heavy price for their risky investments in the mortgage market. The 
SWFs aim to help diversify investments of the countries that they represent and 
to increase their clout in the sectors in which they invest, helping build 
security for a future in which oil is less profitable – or non-existent.<br>
<br>
<b>Strategic move</b><br>
While SWFs such as the Abu Dhabi Investment Authority (ADIA) have swooped to 
support banks hard hit by the credit crunch, MOL was a particularly canny target 
for OOC, given its current feeling of vulnerability due to hostile bids from 
Austria’s OMV and potentially, Russia’s Gazprom, the state-owned energy giant.<br>
<br>
The OOC deal has raised eyebrows in Europe, largely due to an ongoing tug-of-war 
between MOL and its largest single shareholder, OMV, which owns a 20.2 per cent 
stake in the Budapest-based company. OMV, in which the Austrian government and 
Abu Dhabi’s state-run International Petroleum Investment Company (IPIC) have 
stakes of 31.5 per cent and 17.6 per cent, respectively, has long been 
interested in acquiring a majority holding in MOL, specially because it operates 
in many of the same markets in CEE as it does. Last year, OMV had doubled its 
stake in its Hungarian rival and has been aggressively looking to take control 
of the company since then. On its part in a rearguard action, MOL has been 
buying back its own shares. The Hungarian government has also been supporting 
MOL’s moves, retaining a “golden share” and enacting a law preventing firms 
part-owned by foreign governments from taking over majority shares of “strategic 
assets”. It has, however, raised no objections to OOC’s purchase of a minority 
stake thus far. The Austrians, already frustrated by moves to “protect” MOL, are 
livid about the OOC deal, pointing out that it values the shares at considerably 
less than the $186.16 per share that they were offering, albeit conditionally.<br>
<br>
The picture is further complicated by the actions of Gazprom, which is thought 
to be interested in acquiring both OMV and MOL, or seeing them merge in order to 
secure downstream and distribution facilities in the region. According to an 
October report in British magazine, The Spectator, “independent” investors, 
effectively working on behalf of Gazprom, have been buying up shares in MOL, 
some of which have subsequently been sold to OMV. It reported that Gazprom was 
seeking to benefit from a merger between the two Central European companies, as 
the new entity might be forced by anti-trust legislation to divest itself of 
lucrative and strategic downstream assets, which would then be easy for the 
Russian firm to snap up.<br>
<br>
Therefore, the sale to OOC can be seen as part of a concerted effort by MOL to 
defend itself from OMV and Gazprom. As part of this, in December, the Hungarian 
firm had sold a 7 per cent stake to CEZ, the Czech energy firm. And, according 
to one analysis, if the OOC deal goes through, MOL and its “allies” will 
effectively control 44 per cent of the firm.<br>
<br>
However, the agreement is more than merely a defensive manoeuvre for MOL. While 
the details of which assets are to be transferred to MOL have been kept under 
wraps, Zsolt Hernadi, MOL’s chairman and chief executive, was direct about the 
benefits from the deal for his firm. “The Oman deal will give us access to 
different markets,” he said. “This transaction is about creating new markets.”<br>
<br>
<b>High potential</b><br>
MOL may also be eyeing Oman’s work in developing enhanced oil recovery (EOR) 
techniques, or “tertiary” methods of oil extraction, which are deployed when 
natural pressure and gas, and liquid pumping are not sufficient to remove crude. 
Petroleum Development Oman (PDO) has been a pioneer in developing such 
technologies, out of necessity; Oman’s remaining tapped oil reserves are hard to 
exploit, and contain heavier crude with a higher water mix than other reserves 
in the region, making extraction up to five or six times as expensive. However, 
as easily accessible reserves begin to thin out, other countries will 
increasingly have to start turning to EOR, potentially presenting opportunities 
for Oman and its partners to bring their technology in. While PDO is not a part 
of OOC, it is 60 per cent owned by the Sultanate’s government, and often works 
as a partner with the OOC. For example, in refinery projects in Oman. The deal 
thus could meaning potential openings for MOL in EOR activities.<br>
<br>
Neither OOC or MOL are gigantic world players on the scale of Gazprom or Saudi 
Aramco; but their strengths may well dovetail usefully and help safeguard one 
another from their weaknesses (expensive production with a finite timeframe and 
takeover bids from rivals, respectively). While the MOL equity purchased by Oman 
is quite small, it can be an interesting and important step in the 
diversification of Oman’s oil sector – both within and outside the Sultanate’s 
borders. <br>
