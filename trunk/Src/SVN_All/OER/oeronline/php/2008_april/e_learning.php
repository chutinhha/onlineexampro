<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>How do you define e-learning?</b><br>
In simple terms, e-learning is the practice of effectively and efficiently using 
technology to aid the learning process. It’s been around for a while but has 
really come into its own over the last few years and now I think we can 
conservatively say that over 30 per cent of all training is done online. 
Recently, one US company saw a 300 per cent increase in the number of downloads 
of recorded lectures via Podcast and Webcast in a three-month period and I think 
this gives a very typical picture of how things are going.<br>
<b><br>
What have been the major barriers to greater uptake of e-learning in Oman?</b><br>
One thing that can be a barrier to e-learning is the initial costs in setting up 
programmes from scratch. These can be substantial and may act as a deterrent. 
However, once the initial investment has been made, then the returns are huge – 
not only in terms of cost-effectiveness but also in terms of learning outcomes, 
in terms of reach, in terms of improved job performance, and let’s not forget 
job satisfaction. I think many people these days take continuous professional 
development for granted and e-learning is a great way of delivering it, of being 
able to offer more choices and at a cost that is viable. <br>
E-learning has economies of scale; the more people take up the courses, the 
cheaper they get.<br>
<br>
Of course, the lack of familiarity with technology initially held things back in 
Oman – this was also true worldwide – but time and a generation that has grown 
up with computers and mobile phones are changing that. So things are very 
different now.<br>
<br>
<b>
<img border="0" src="e_learning2.jpg" width="270" height="281" align="right" hspace="6" vspace="4"></b>This is linked to another problem. Initially, as people didn’t really understand 
the possibilities of technology, there was the tendency to transfer classroom 
course material direct to the screen, or transferring existing “training manual” 
content to an e-learning form. Neither of these is particularly engaging and 
results in nothing more than a lengthy “screen-reading” exercise on the part of 
the student. In fact, they do not represent a proper e-learning experience at 
all. They’re far too inflexible. Understanding that the online environment can 
supply a far more immersive and engaging experience to the user is the first 
step towards a successful e-learning roll-out. Thankfully, this is very 
different now and what’s being used is really interactive and things like 
weblogs and videoconferencing are common, Just look at Second Life, that’s an 
amazing learning environment. E-learning is a whole new learning model, more in 
tune with the learner and his or her needs.<br>
<br>
This brings us to the problem of lack of local content and here, with a bit of 
thought, a real difference can be made. After all, e-learning is about meeting 
the needs of the learner more exactly and there’s so much that can be done here, 
specially in terms of relating to local culture and in terms of materials in 
Arabic – which certainly will open up e-learning to many more people.<br>
<br>
Of course, internet penetration and speed are still a problem here. We do need 
to ramp up usage of the web and its speed. Though if we’ve got attractive and 
appealing local e-learning content, then that is going to help drive this.<br>
<b><br>
How do you feel Oman’s public sector can benefit from e-learning?</b><br>
Cost is a major factor. E-learning is without a doubt a very cost-effective way 
of delivering training material to large groups of users, once it has been 
properly set up. An online repository can also ensure that everyone is 
up-to-date on training information and other relevant materials. Investment in 
e-learning will bring lasting benefits.<br>
<br>
But I think you have to look at this question in another way too. A major 
constraint in economies of all types is the supply of well-educated people, that 
is those with the skills and know-how to meet the demands and needs of the 
knowledge economy. E-learning definitely has a role to play and I think it’s not 
a case of how can the public sector benefit but more a case of what will happen 
if it doesn’t take full advantage of it.<br>
<br>
<b>Do you think that e-learning is more readily accepted today?</b><br>
<b>If so, what do you think this tells us about the direction we’re heading to 
as a nation?</b><br>
As a nation, we’re getting more and more dependent on accessing and receiving 
information on demand. Internet penetration is getting deeper and 3G internet 
access on your phone will not only bring the internet to more people, it will 
make the “anywhere, anytime” mantra of e-learning a reality for many more 
people. Today, folk in Oman bank online, book airline tickets online, pay their 
bills online and of course, check their speeding fines online. It’s only a short 
step to more people taking accredited courses online or improving our job skills 
online.<br>
<br>
It’s interesting but unlike most places which have moved gradually on e-learning 
with a PC or laptop, I think in Oman the big leap is coming through mobile 
phones. Internet access through your mobile is I believe a big step forward for 
this country, and as this develops, you really will see an enormous growth in 
e-learning. Of course, it always comes back to the “Nintendo” generation – young 
people who know their way around technology.<br>
<br>
<b>How does Oman compare to other Gulf countries in e-learning awareness?</b><br>
From a regional perspective, I don’t see any one particular Gulf country taking 
the lead in the e-learning space. The paperless Zayed University was a great 
initiative. The success of an e-learning project depends entirely upon a 
sensitive and well-thought out execution, combined with carefully crafted and 
managed e-learning content. I think over the next two to three years, we’ll see 
a substantial increase in the use of e-learning – in schools, colleges and 
corporates.<br>
<br>
<b>You have a particular interest in gaming and its application to learning – how 
do you see these two themes co-developing? </b> <br>
I was pleased to see that KOM has placed gaming and its applications to the 
learning environment on this year’s eGames Conference agenda. Without a doubt, 
some of the presenters will be talking about the fact that students are well 
ahead technologically and it is our role to keep up or stay one step ahead. 
Indeed, as I’ve already mentioned, it’s more than apparent to me that most 
Oman-based kids are swamped. In fact, they are immersed in an incredibly 
visually rich and technologically advanced world, of which in some respects we 
have a limited understanding. They have insights into technology that we can’t 
even begin to imagine. I think it’s our responsibility to harvest some of the 
gaming tools that kids are using. In a learning context, I really like the idea 
of kids being able to use the things they’re comfortable with, this can only 
enhance their learning experience, making it more meaningful and rewarding. It’s 
a win-win situation for everyone, the kids, schools, parents, colleges and 
eventually the employer.<br>
<br>
<b>Where do you think e-learning will be in five years?</b><br>
By 2012, the majority of learners will see not only their PC but also television 
to gaming consoles and even mobiles as gateways to learning and essential tools 
for information gathering and communicating. The teacher’s role will focus more 
on ‘managing the learning’ rather than being the knowledge focal point. People 
are already talking about integrated and blended learning, and that’s a theme 
that just won’t go away.<br>
<br>
One-to-one learning is already becoming the next big thing in the e-learning 
space. Community platforms are also becoming a norm, with group work across 
distances as simple as being in the same room and the transfer and broadcast of 
learning a systemic process.<br>
<br>
Let’s see if we develop like Korea where e-learning is a real phenomenon. One 
particular e-learning provider there is currently trading at more than 20 times 
its estimated 2007 earnings based on market consensus and is one of Korea’s 
hottest stocks. And watch out for Korean-style superstar teachers, where some 
tutors have become celebrities with one popular English language instructor 
earning more than US$2 million last year! </font></p>

