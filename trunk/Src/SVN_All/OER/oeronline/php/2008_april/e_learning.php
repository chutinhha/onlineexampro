</font><b><font face="Verdana" size="4">Learning Net</font></b><font face="Verdana" size="1"><br>
<b>OER talked to Karim Rahemtulla of Knowledge Oasis Muscat-based <br>
Infocomm company, about the future of e-learning, the barriers it faces and how 
it can help meet the growing training needs of Oman’s public and private sectors</b></font></p>
<table border="0" style="border-collapse: collapse" width="100%">
	<tr>
