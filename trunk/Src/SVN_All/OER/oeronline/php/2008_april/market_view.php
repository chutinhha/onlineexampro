<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana">
<br>
</font><font face="Verdana"><font size="4"><b>Leading from the front</b></font><font size="1"><br>
<b>The MSM30 has not only gained in a period of global meltdown but also 
exhibited high liquidity</b></font></font><font size="1" face="Verdana"><br>
<br>
<br>
MSM30 Index’s performance was an indication of the insulation of domestic 
markets from the sub-prime crisis in the US. MSM30’s year to date (YTD) gain of 
almost 19 per cent, during the global market meltdown last month stood out and 
was the best performing index in the world (source: Reuters). MSM30 index gained 
11.5 per cent between February 16 and March 15, on the back of strong results 
and high dividend payout by companies.<br>
&nbsp;<br>
High liquidity in the market was well reflected in the average daily turnover, 
up by 35 per cent to RO18 million per day during this period. Some of the top 
performers were ONIC Holding (+34.5 per cent), Al Anwar Ceramics (+30.5 per 
cent), Al Jazeera Steel (+27.8 per cent) and OIFC (+22.2 per cent). Strong 
results and robust cash and stock dividend announcements by companies pulled up 
the index. High dividend paying companies were Omantel, Shell Oman and Raysut 
Cements, all paying dividend of 100 per cent.<br>
<br>
In corporate developments, Oman Telecommunications Company (Omantel) and Oman 
Mobile Telecommunications Company (Oman Mobile) announced the integration of 
both the businesses, a process expected to be completed by the end of FY08. The 
company expects to capitalise on the synergies of both the businesses through 
building internal capabilities and improvement of operational efficiencies. In 
other corporate events, the boards of directors of Muscat Finance and National 
Finance approved the merger of both the companies. Based on E&amp;Y valuation, 
shareholders of Muscat Finance would own 57.83 per cent of the merged entity 
while those of National Finance would own 42.17 per cent.<br>
<br>
In result announcements, Galfar announced a 62 per cent year over year (yoy) 
revenue growth and 39 per cent yoy increase in profit for FY07.<br>
<b><br>
GCC MARKET COMMENTARY</b><br>
Most of the GCC bourses ended on a positive note for the month ended March 15. 
The Saudi stock exchange grew by 5.56 per cent during the period, primarily on 
the back of buying seen in industrial counters, with the industrial index rising 
by 11.08 per cent. The Qatari bourse ended up 4.46 per cent, primarily due to a 
rise in banking and services’ stocks, which increased by 6.91 per cent and 2.96 
per cent, respectively. The Kuwaiti index was up 4.07 per cent during the 
period, with most of its sub-indices ending in green. Its banking and services 
indices were among the ace performers, gaining 6.48 per cent and 6.02 per cent, 
respectively, during the period. <br>
<br>
The Dubai and Bahrain stock exchanges ended almost flat, with the general 
indices increasing by 0.32 per cent and 0.27 per cent, respectively. While real 
estate companies topped the Dubai bourse, banks were among the chief gainers in 
the Bahraini market. The Abu Dhabi market, however, was the only negative 
performer during the month, with its general index shedding 0.87 per cent. The 
banking sector was among the worst performers on the exchange, with the banking 
index down 1.55 per cent.</font><p align="justify" style="margin: 3px 10px">
<font size="1" face="Verdana">
<br>
<b>RESEARCH HIGHLIGHTS</b><br>
Oman Investment and Finance Company<br>
Oman Investment and Finance Company’s (OIFC) business model provides good 
revenue visibility, with existing contract periods averaging at about 
two-and-a-half years. OIFC has also illustrated its ability to retain existing 
clients without too many negotiations. A strong network in Oman amidst growing 
economic activity further strengthens the company’s bid for future client 
additions. Its major source of revenue (approximately 56 per cent) comes from 
the factoring business for Omantel. The company is involved in bill printing and 
bill collection from fixed line telephone, global system for mobile (GSM) 
communications and internet consumers on behalf of Oman Telecommunications 
Company and Oman Mobile Company. In recent times, OIFC has been able to 
diversify its client base, adding Oman Mobile for selling Hayyak starter kits 
and prepaid cards and Oman Qatari Insurance Company for marketing insurance 
products. For the nine months ended FY08, OIFC reported operational revenues of 
RO6.30 million, up 6.9 per cent yoy while net income (excluding non operating 
income) grew 14.1 per cent yoy to RO1.85 million.<br>
