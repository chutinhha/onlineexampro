<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana">
<br>
OIFC has been able to expand its operating margins from 21 per cent in FY05 to 
33 per cent in the first nine months of FY08. The company’s growth in billing 
from Omantel and MHEW has been a major driver in this. Ongoing infrastructure 
development projects, economic growth and increased population in the Muscat 
region are all indicative of a further rise in consumption of water and 
electricity. Water distribution was badly affected during cyclone period in June 
and July 2007; hence, there was a slight drop in distribution of water in 2007 
compared to 2006. However, monthly figures show a significant uptrend.<br>
<br>
Trading at a 15 per cent discount to our target price of 688 Bz, and with 
approximately 5 per cent (expected) dividend yield, the shares of OIFC seems to 
be a good investment opportunity. We believe the company has the wherewithal to 
add more clients to further leverage its reach and strategic position in the 
debt factoring business. Our estimates may prove to be conservative, if the 
company opens any fresh channels or adds new clientele.
