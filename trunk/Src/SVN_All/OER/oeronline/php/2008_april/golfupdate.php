</font><b><font face="Verdana" size="4">Golf Scores High</font></b><font face="Verdana" size="1"><br>
<b>Many new courses are under development, indicating a significant growth for 
golf in the Middle East region</b><br>
<br>
<img border="0" src="golfupdate,jpg.gif" width="300" height="239" hspace="6" vspace="4" align="left">Golf may be new to the Middle East, but it has gained immense popularity within 
a short span of time. Two decades ago, when the first full grass 18-hole course 
opened in Dubai, nobody could have imagined that one day it would find its place 
in the international golfing calendar. Today, Dubai hosts major global golf 
championships and from next year onwards, it will hold the richest tournament in 
the world, at the Jumeirah Golf Estates. Whether a serious golfer or an amateur 
enjoying his shots, Dubai today caters to everybody’s needs. Most top managers 
and entrepreneurs have a penchant for playing golf in their leisure to 
rejuvenate themselves.<br>
<br>
It is in the UAE where half of the golf courses in the Middle East region can be 
found, followed Saudi Arabia and Qatar, Lebanon, Syria, Kuwait and Bahrain, in 
that order. Here, Abu Dhabi has emerged as a high-profile golfing destination – 
between 270,000 and 300,000 rounds are played annually in Dubai. Though the 
number of courses available in the Middle East is still very small, many new 
courses are under development, indicating a significant growth for golf in the 
region. At this juncture, one question that is often raised is that: What 
business potential does golf have in the Middle East? But if one looks at the 
properties attached to golf courses, one discovers that all command a premium, 
making them a great potential investment for buyers and a peaceful location for 
living.<br>
<br>
<b>Profitable business</b><br>
Many corporate captains feel that developing a golf course is expensive, as it 
is land-intensive, water-intensive, capital-intensive and time-consuming. Very 
often, the costs of developing a golf course can vary between $30 million and 
$40 million, and recouping those costs can take years. Hence, to make golfing a 
profitable venture, the idea of clubbing golf courses together with real estate 
development is fast gaining popularity. Support from the government is also 
becoming imperative, as the benefits of such support percolate sectors of 
hospitality, tourism and retail.<br>
<br>
According to a recent report by KPMG Advisory, the annual membership fees in the 
Middle East was around US$3,741 per individual in 2007, with fees in Dubai 7 per 
cent higher. For one golfer to come and play one round, he needs to fly in, stay 
two nights in a hotel, dine and sometimes buy a shirt and shoes. That is why the 
real estate, tourism, retail and hospitality sectors have become big 
contributors to the development of the golf industry. In fact, golf is becoming 
a big business now, with the KPMG report pointing out that in 2007 alone, golf 
revenues, including food, drinks and retail, reached US$6.7 million, with 
revenues at Dubai courses hitting US$8.8 million.<img border="0" src="golfupdate2.jpg" width="199" height="228" align="right" hspace="6" vspace="3"><br>
<br>
Contributing to the emergence of this industry, experts say, has been Dubai’s 
exemplary marketing approach, which placed the region on the global golfing map. 
As a result, annual golf events are now celebrated on a large-scale, attracting 
their own set of golfers and viewers. “Putting together a hospitality programme 
with a golf tournament is a powerful tool through which the sponsors of the 
tournament can bring in their best clients to experience the beauty of the 
place, stay in the great hotels and play golf,” says Greg Sproule, Managing 
Director of sports media and entertainment company IMG.<br>
<br>
<b>Futurescope</b><br>
While Sproule admits that it is great to have private high-end clubs, 
tournaments that play a big role in the property equation. Similarly, Middle 
Eastern countries can further their golf business through segmentation by 
establishing what’s known as ‘Pay for Play Golf’. Sproule believes this concept 
will make golf affordable to the locals and the tourists, as well.<br>
<br>
However, this concept is new and yet to gain currency. Until then, the Middle 
East will continue to see an increasing number of luxurious golf clubs that are 
connected to real estate and top business around the region. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
