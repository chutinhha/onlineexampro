        <p align="justify"><font face="Verdana" size="1"><br>
		</font><b><font face="Verdana" size="4">Relating to people around</font></b><font face="Verdana" size="1"><br>
		<b>Paul Spyropoulos, Country Managing Director of Bahwan DHL Exel Supply 
		Chain, Oman, takes the supply chain philosophy seriously<br>
		</b><br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="lastpage.jpg" width="174" height="238" align="left" hspace="8" vspace="6"></font></b><font face="Verdana" size="1">Born in Kenya, Paul Spyropoulos, the Country Managing Director of Bahwan 
		DHL Exel Supply Chain, is half Greek and half Irish. “That allows for 
		the odd name,” he says in all candour. It is this candour that indicates 
		that he has an attitude that surely helps people around him lighten up.<br>
		<br>
		Paul has been with the DHL business for the past eight years. DHL is the 
		global market leader of the logistics industry. The company expertise 
		includes express, air and ocean freight, overland transport and 
		international mail services. Paul started his career with Tibbett in 
		Britain, later acquired by Exel and taken over by DHL, a couple of years 
		ago. <br>
		<br>
		Having arrived in Oman three months ago, Paul is here to stay as the 
		chief whip of the new corporate brand, Bahwan DHL Exel Supply Chain, a 
		joint venture between Bahwan Cybertek and the DHL Exel Supply chain. The 
		company will provide complete supply chain solutions along with 
		logistics solutions, using the latest technologies, supported by best 
		industry practices.<br>
		<br>
&nbsp;The aim is to support Oman’s economic boom, 
		specially in the oil and gas sector. Very enthusiastic about the 
		prospects of the venture in Oman, Paul says, “Oman’s economy is 
		expanding rapidly, boosted by significant oil exploration, production 
		and refining activities. These industries, alongside other buoyant 
		sectors, call for the best supply chain solutions. Bahwan DHL Exel 
		Supply Chain is capable of providing clients with both third and 
		fourth-party logistics solutions for their total supply chain, ranging 
		from consulting and design to management and execution.”<br>
		<br>
		At the moment, for Paul, a typical working day is a 11-12 hours’ grind, 
		as the job demands. His company’s key activities in Oman are to provide 
		4 PL logistics services to PDO. In addition, the core areas of focus are 
		around rig moving and cargo and water haulage. Being the country 
		managing director of a multinational’s operations in Oman is no 
		cakewalk, what with extended working hours and stress being a constant 
		factor? “I think it is all about getting your balance right,” comes the 
		self-assured reply.<br>
		<br>
		In his spare time, Paul is at home, the best place to unwind, with the 
		companionship that family life brings. His two-year old daughter keeps 
		him on his toes. <br>
		<br>
		Paul himself enjoys fishing, rugby and camping. As for rugby, Paul is 
		involved as a spectator, no longer as a player. It does seem a natural 
		switch, as it is difficult to play team games after one is 
		professionally engaged. Fishing, which he started quite young, is still 
		a favourite pastime. Paul has already done some fishing in Oman, which 
		has proved fairly successful.<br>
		<br>
		Paul is an outdoor person, so is his wife and daughter. They love the 
		sea and outdoors. With pristine beaches and stunning mountains, Oman 
		offers an irresistible combination that makes him feel good about his 
		environment. Before taking up his present role, Paul was the 
		country-director for Uganda; and prior to that a business director in 
		sub-Saharan Africa, where he focused on providing supply chain solutions 
		to the beverage and tobacco sectors. Moving to Oman and getting involved 
		in the oil and gas sector is a wonderful opportunity, though Paul says 
		his first three months have been a very steep learning curve.<br>
		<br>
		A very approachable person by temperament, Paul shared his take on 
		managing relationships, internal and external business relationships and 
		personal relationships with us. The key ingredient to any relationship 
		is making sure that it is a win-win situation for all. One should have 
		the ability to see the position of both sides and build relationships 
		around that. It is interesting to note that Paul has a good command of 
		Swahili, which is widely spoken in Oman.<br>
		Positive as always, he says, “I am keen to make sure that we can give 
		the best service as Bahwan DHL to our key customers, specially PDO. I 
		hope to play a full part in Oman’s social scene. I look forward to 
		meeting many people and I am sure that my family and I will be very 
		happy in Oman.”<br>
		<br>
		-<b>Susmita De </b> </font></p></td>
