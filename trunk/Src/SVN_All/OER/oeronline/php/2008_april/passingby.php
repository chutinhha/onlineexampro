<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">On the Prowl</font></b><font face="Verdana" size="1"><br>
<b>Ramesh Kumar met up with investment banker Nigel Sillitoe offering ‘boutique 
managerial services’ in Muscat<br>
</b></font><font face="Verdana" size="1"><b><br>
</b><br>
Next time if you bump into forty plus, athletic Nigel Sillitoe in the corporate 
corridors of Oman, resist the temptation to talk big numbers like billions and 
trillions. His investment banker look is bound to tempt one to adopt such 
postures. If you do, be ready for a wry smile. Rather a smirk. Know why? He 
simply believes in the ‘small is beautiful’ credo. Hang on, he is not a Joseph 
Schumpeter fan! Having seen and handled billions and trillions, they no longer 
excite him! Strange, no doubt.<br>
<br>
By the way, your chances of crossing paths with this Dubai-based Londoner are 
pretty big as he is knocking on the doors of established banks, pension funds 
and family offices to garner money for his new employer – Thames River Capital 
LLP. That’s I how bumped into him during his recent trip to Muscat – his 20th in 
the past seven years.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
Still in a quandary over Nigel’s coordinates? No issues. Here they are. Nigel is 
a valued asset manager whose services have been sought and utilised by giant 
asset management companies such as HSBC Natwest, Framilington, Arbuthnot, 
Mellon, etc. Pretty, long list. “Been there – and done it – for a quarter 
century,” remarks the March 1961 born Thames River Capital representative 
currently stationed in Dubai as Executive Officer for Middle East. A 
back-of-the-envelope calculation reveals that the total assets under management 
(AUM) during his 25-year reign is an estimated US$1.2 trillion! “May be, a 
little above than that,” he adds modestly, but hastens to deny when I flash two 
– indicating two trillion dollars.<br>
<br>
<img border="0" src="passingby.jpg" align="right" hspace="6" vspace="4" width="151" height="172"><b>Oman Odyssey</b><br>
What brings Nigel to Oman? Money, of course! Approximately US$2.1 trillion is 
currently under AUM in the GCC region, though some estimates peg this at US$4.1 
trillion. Abu Dhabi leads the table with US$900 billion, following by Kuwait 
(US$200 billion), Saudi Arabia (US$200 billion), Qatar (US$60 billion), followed 
by Bahrain and Oman. The Sultanate, according to him, boasts of US$20 billion 
roughly through a multiplicity of pension funds and others. Too many pension 
funds do not excite him. Nigel has found out that of the 800 family offices 
(read privately-owned, unlisted corporate entities) in the entire GCC region, 
approximately 10 per cent of them are Oman-grown. No wonder, Oman is an 
important port of call. You name the big family-run businesses in Oman, he has 
met them already. Nigel is cagey about sharing their names. Understandable.<br>
<br>
<b>Boutique Route</b><br>
Thames River Capital’s ‘Storm GCC’ campaign is crystal clear. “Family offices, 
loaded with money, will invariably look for big banners to park their funds and 
want money management. However, over the past few years, many of them learnt 
that such big banners mean a lot of bureaucracy and no big returns. This 
automatically led to them scouting for nimble-footed, experienced, 
performance-guaranteeing asset managers under a smaller umbrella. No big 
banners, but big guns with tonnes of experience in managing money smartly and 
wisely. We are here to provide such ‘boutique managerial services’,” elaborates 
the Londoner.<br>
<br>
To put it in a different perspective, big investors are not a disillusioned lot 
when markets boom and money managers turn huge dollops of manna. The kick in 
comes in when indices dive southwards and money managers find it hard to cover 
the downside adequately. Remember, ‘adequately’ is the operative word. Plus, 
less bureaucracy. More interpersonal relationship between potential big 
investors and money managers, perhaps. That’s the gap Nigel’s London team tries 
to bridge as a unique selling proposition. Nothing unique, honestly, but there 
are takers for such hard or soft sell, which helps Nigel keep going.<br>
<br>
The market is ripe, if Nigel is to be believed. Under legendary Charlie Porter 
as Chief Executive Officer at the London headquarters, a 160-member team 
services select family offices across the globe. Half of this lot consists of 
seasoned money managers, recruited only through internal referrals. No 
headhunters, please! “We all have worked with one another in the past in one 
place or another. Each one is a name to reckon with,” announces a flamboyant 
Nigel. Can he name one to quench my curiosity? “Heard of Ken Kinsey Quick from 
South Africa? He’s one of the best hedge fund managers,” pat comes the reply. 
What’s left unsaid is ‘in the world’. Porter, for information, is an ex-Barings 
veteran. Super league, no doubt.<br>
<br>
The icing on the cake is that none, repeat, none, of the 80-member money 
management team has left River Capital since joining. “We are excellent at 
poaching talent from everywhere,” guffaws Nigel at his company’s reputation: “By 
the way, nobody leaves Thames. The question does not arise at all. We are here 
for our own benefit,” he adds. Come again, I demand. He repeats the same. Still 
it does not make any sense. Can he elaborate to drive home the point? Sure, why 
not? he volunteers.<br>
<br>
Thames River Capital allows money managers to park their own money in the funds 
managed by them for their high value clients.<br>
<br>
<b>Eternal Optimist</b><br>
Picking up courage, I ask him, what has he achieved in the Sultanate? He ignores 
the question, quietly shuffling around the cellophane-wrapped magazine lying on 
the table in front of him.<br>
What’s the immediate objective? Pat comes the reply: “By April, I will have a 
cheque from Oman to show (it) to Charlie.” And a fresh load of passionate work 
for one of the eight teams on Barclay Square, London. Any bets? No hedging 
please. <br>
