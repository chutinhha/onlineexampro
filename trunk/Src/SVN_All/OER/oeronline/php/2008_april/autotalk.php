        <p align="justify"><font face="Verdana" size="1">
        <br>
        </font><b><font face="Verdana" size="4">Stylish Cruiser</font></b><font face="Verdana" size="1"><br>
        <b>The Tribeca is Subaru’s first ever foray into this segment in Oman, 
		and OER get to drive it first. Malcolm X Crosta reports</b></font></p>
        <p align="center">
        <img border="0" src="autotalk6.jpg" width="445" height="341"></p>
        <p align="justify"><font face="Verdana" size="1"><br>
		Fuji Heavy Industries (FHI), the maker of Subaru automobiles, unveiled 
		the new Tribeca at the 2008 New York International Auto Show. This is 
		actually a face-lifted version of the existing Tribeca, which is not 
		available in Oman. In unveiling the new version, Subaru seems to have 
		learnt from experience and has further improved on everything that was 
		good about the original.<br>
		<br>
		The original Tribeca had very controversial external styling. It was the 
		kind of design that is either hated or loved, with no middle ground. So 
		this time around, they have taken the safer, more contemporary route. It 
		now has a very simple and straightforward look, one that works. The only 
		minor downside to this design change is that the approach angle has been 
		reduced to 16.7 degrees, from the previous generation’s 18 degrees.<br>
		<br>
		<b>Inside</b><br>
		Subaru had got the interior right the first time round, so they didn’t 
		want to mess around too much with its success. The Tribeca is very 
		stylish, with its curved dash and aluminium accents, and at the same 
		time very practical and functional, save for a centre stack that curves 
		away from the driver and a few ancillary gauges that are blocked by the 
		driver’s hands.<br>
		<br>
		Space in the front and back is more than adequate, even for 6-footers. 
		Also, access to the third row has been improved over the previous model, 
		with the addition of a tilt-and-slide feature on the driver-side 
		second-row seat to complement the existing one on the passenger side. 
		There is also a stronger helper spring to make the process of getting 
		back there easier. Unfortunately, like most of the other models in the 
		same class, the third row is a children only section. Those of larger 
		dimensions can find more than enough room in the second-row seats, which 
		feature eight inches of fore-and-aft travel and also recline. To add to 
		this, refinement has also been taken up a notch, with reduced exterior 
		noise levels, including engine noise, making long drives rather 
		comfortable.<br>
		<br>
		The Tribeca is available in only one standard trim level in Oman and it 
		comes with loads of features. Leather seats with power adjustments, 
		individual rear cooling system, Subaru’s own nine-speaker premium audio 
		and a lot more are included.<br>
		<br>
		<b>Drive</b><br>
		The on-road performance of the older model was good enough but lacked 
		power. That is why Subaru has taken the existing engine and given it a 
		whopping 20 per cent increase in displacement. The engine now displaces 
		3.6 litres and puts out 256hp and 247lb-ft of torque, an increase of 
		11hp and 32lb-ft over the previous engine.<br>
		<br>
		To be on the safer side, Subaru has given the existing five-speed 
		automatic transmission a thorough makeover. Taller gear ratios in the 
		revised, lighter gearbox keep shift points reasonably similar to the 
		previous transmission. Meanwhile, revised shift mapping and a new torque 
		converter are said to deliver quicker, smoother shifts. These gearbox
		improvements, along with the bump in power by the revised engine, allow 
		the Tribeca to hit 100kmph in about 8.9 seconds, which is a reasonable 
		improvement.<br>
		<br>
		The suspensions do a good job of keeping the vehicle smooth over bumps 
		and ditches, even at high speeds. Even the roll is kept to a minimum. 
		The Tribeca is not as sporty as you might expect, from an AWD Subaru, 
		but it is very competent, comfortable and easy to drive.<br>
		<br>
		<b>Verdict</b><br>
		The Tribeca is a very straightforward, simple yet stylish vehicle. But 
		unlike most Subarus, it is more suited towards cruising as opposed to 
		being sporty. It is not exactly cheap at a price of around RO14,000 but 
		considering the fact that there is only one trim level available, it 
		matches other similar models in its class. Anyone in the market for a 
		spacious, modern vehicle for city use and for an occasional drive to 
		Dubai should look in the direction of the Subaru.<br>
