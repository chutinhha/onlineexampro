<p align="justify" style="margin-left: 10px; margin-right: 10px">
<b><font face="Verdana" size="4">
<br>
‘‘It’s important to network’</font></b><font face="Verdana" size="1"><br>
<br>
<img border="0" src="inthenews4.jpg" width="200" height="244" align="right" hspace="6" vspace="3">Diane Girard, the founder and president of Global Links Network, an 
international trade facilitation firm, specialising in trade, training and 
technology, is first and foremost an entrepreneur. Since founding the company in 
1991, Diane has consistently focused on positioning the company and its 
associates for the future world of trade and development – a world which 
increasingly involves technology, the export of services, and is driven by new 
global trades, such as micro-enterprises, women and young entrepreneurs. Trade 
through technology is her corporate motto and also her company’s strategy for 
working in transitional and developing economies.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<br>
Daine took part in the Women in Business Conference and conducted an inspiring 
workshop that showcased the formula for success in any business setting. Oman 
Economic Review caught up with her on the sidelines of the event for a brief 
tête-à-tête on the increasing significance of women in business. Excerpts:<br>
<br>
<b>What pointers do you have for women who are striking it out on their own in the 
corporate arena?</b><br>
I’ve had the wonderful opportunity to see three different presentations during 
the two-day conference, one of which was a workshop titled ‘Essential Business 
Skills – A Woman’s Secret Formula’. It talks about a lot of things such as 
setting goals, building confidence, networking and meeting people. A lot of 
women are not accustomed to being involved in a business environment and it’s a 
challenge for some women to even go into a room that is filled with men, or 
filled with women or both. Because they haven’t been involved in a business 
environment, they don’t know how to get started. And the other thing is that 
they will tend to go towards the person(s) they know and stay with them. And it 
is specially important that when you go to another country to meet people you 
need to circulate and network because time is money. But, we find quite often 
that people who go as a delegation of three or four businesswomen will group 
together and people tend to want to be comfortable. So, we try to help them 
break out of that habit and to have the confidence to network and to know 
exactly what to do when they go into a room and how to present themselves.<br>
<br>
Another thing is that with technology, anything is possible. So I was here to 
focus on how to use that technology and talk about how it can save time and 
money, and help build linkages. We talked about partnering as well, because 
entering into a new business venture is difficult if you’re doing it on your 
own, and finding the right partner is really important. It’s not really about 
entering into a business partnership, but it is partnering more in the loose 
sense of the word. For instance, set up a partner for a specific project or 
market or product. It’s important to find someone with the same values as you 
specially someone with complementary skills. Also, when you start with a 
partner, start small, start with a small project, see how the relationship is 
going to evolve, so you don’t have as much at risk. Then there’s less stress and 
less potential of financial loss. Be sure to nurture it and spend the time to 
develop it, specially if it is an international partnership. Having a local 
presence is very important; particularly with someone who can open up the 
market, someone who already has established contacts.<br>
<br>
<b>Are more women stepping out on their own?</b><br>
Yes, absolutely, there’s no question about it. Even in Canada, we’ve seen a big 
shift in the last 20 years or so. And it’s amazing in Canada to see the number 
of women who own businesses. We have a population of about 32 million people and 
about 800,000 of them are women entrepreneurs, so that is one in 32 people 
really. Women are increasingly going global. And that’s what I’ve been doing as 
well for the past number of years. I started in international business in my 
early twenties and so the biggest challenge for me has been the new market that 
I can open up; where I can go next; and where I haven’t had an opportunity to do 
business in a particular region/country. More and more women are looking towards 
global markets and it’s primarily because of technology, it is possible to do 
it. Everyone has an email account, so many people are on Facebook and have their 
own blogs that it is possible now to build relationships, which were very 
diffuclt a few years ago. Then, you’d go to a conference and you’d have to rely 
on what we call snail mail and very expensive long distance calls, so it was 
very difficult to continue to build a relationship. Now with technology, you’re 
at the same table even when you are world’s apart.<br>
<br>
<b>Do you anticipate areas of partnership between Omani businesswomen and your 
international network?</b><br>
I am very interested in continuing to do business here. The businesswomen I’ve 
met so far feel like sisters already. My area of expertise is in the business 
and professional services sector and it’s an area that I speak to audiences 
about in many parts of the world. When people think about international 
business, they think about shipping products but services can also be exported, 
so that is an area that I will look for partnership in Oman. We have educators, 
people in the health and wellness sector, doctors, lawyers, so those are 
services where people really are exporting their knowledge not goods. <br>
<br>
<b>What are some of the key considerations for business that women need to know?</b><br>
Triple bottom line (TPL) is something that women need to be aware of. Usually, 
people think about the bottomline in terms of money but triple bottomline refers 
to the financial bottomline but also the socialbottom line and the environmental 
bottomline. So, its about having policies that have ethics in mind, with gender 
equality. TPL is something that women are specially conscious about, so it’s 
important for businesswomen to position themselves that way.</font></p>
