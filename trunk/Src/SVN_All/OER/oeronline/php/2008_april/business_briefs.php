</font><font size="2" color="#CC3300" face="Verdana"><b>Oeronline.com gets Oman 
Web Award</b></font><font face="Verdana" size="1"><br>
</font>
<img border="0" src="business_briefs12.jpg" width="330" height="157" align="right" hspace="6" vspace="3"><font face="Verdana" size="1">www.oeronline.com, the premier business news website in Oman, has won the Oman 
Web Award in the media services and event management category. The website is 
designed by UMS Interactive and part of UMS Group. Oman Web Awards 2007, the 
Sultanate’s answer to globally renowned Webby Awards, was held at Muscat on 
March 8. OWA is a member of the Pan Arab Web Awards and serves as an inspiration 
to the web designing companies, designers and freelancers in Oman. Speaking on 
the occasion, Pierre T. Moukarzel, President of Pan Arab Web Awards, said, “For 
Oman, it is the third web awards and the event has witnessed an overwhelming 
response this year”. OWA were supported by Information Technology Authority (ITA), 
the public authority set up by the government to implement Oman’s digital 
strategy through e-governance and nurture the knowledge society in the 
Sultanate. Overall, 176 websites were in fray for Gold, Silver and Bronze awards 
in 23 different categories. TBWA\Zeenah was given the most coveted prize ‘Best 
Web Company of the Year’ based on its record winning entries in the competition.<br>
