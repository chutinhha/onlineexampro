<font size="2" color="#CC3300" face="Verdana"><b>NTI BizPro Awards 2008 
felicitates Omani talent</b></font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="business_briefs.jpg" width="200" height="136" hspace="6" vspace="3" align="right">The NTI BizPro Awards 2008, held in March, motivated and appreciated the 
positive achievements of young Omani managers. They were also aimed at the 
development and encouragement to the leadership skills in young Omanis. The 
three young achievers showered with accolades were: Baha Mohammed Ridha 
Nooruddin Al Lawati and Asim Mohammed Bahram Al Zadjali, represented ‘Omantel’ 
while Fatma Khalid Al Siyabi was from ‘Shell Oman Marketing Company’.<br>
<br>
The NTI BizPro Awards 2008 also honoured Dr Amer Awadh Al Rawas, managing 
director of Oman Mobile, with the ‘Business Leader Award’ for his contribution 
to the organization and the Sultanate. Lawrence Alva, general manager, NTI, 
stated: “Competence is a combination of knowledge, skills and attitude and 
cannot be taught at a business school, it has to be practised and nurtured. All 
the participants of the BizPro Award have demonstrated the grit and 
determination to succeed and grow. They are all winners in the true sense of the 
word.”<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Origin Oman campaign 
launched</b></font><font face="Verdana" size="1"><br>
To promote the local manufacturing industry, Oman has initiated a special 
promotion campaign ‘Origin Oman’. According to HE Ahmed Al Dheeb, Under 
Secretary for Commerce &amp; Industry, Ministry of Commerce &amp; Industry, “Origin Oman 
is a new think, buy and eat local business initiative from the Public 
Establishment for Industrial Estates (PEIE). The initiative aims to preserve and 
enhance the economic, human and natural vitality of Oman’s communities by 
promoting the importance of purchasing locally made products and services”. As 
part of the campaign, PEIE has also launched a dedicated portal 
www.originoman.om which facilitates the online business matching of local and 
foreign enterprises by allowing them to create and view company profiles, trade 
leads and product information spanning a wide range of industry sectors. 
</font>
<p align="justify" style="margin: 3px 10px">
&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">Omani 
enterprises will also be alerted by SMS of appropriate matches, allowing local 
and foreign businesses to make those first connections with each other.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Muscat Golf and Country 
Club rechristened ‘Muscat Hills’</b></font><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>
<img border="0" src="business_briefs3.jpg" width="250" height="162" align="left" hspace="6" vspace="3"></b></font><font face="Verdana" size="1">The Muscat Golf and Country Club, among the first major real estate development 
projects announced in Oman, is back in news. Rebranded as “Muscat Hills”, a 
cherished dream of His Highness Sayyid Kais bin Tarik Al Said, the project 
promises to bring the first PGA-standard 18 hole Par 72 championship green golf 
course in Oman. The golf course is designed by the internationally renowned Paul 
Thomas of Dave Thomas Associates and promises to integrate the sporting demands 
of a high quality championship course with the natural beauty of the Omani 
landscape. Chandra Lahiri, Group Managing Director of Muscat Hills, said: 
“Muscat Hills comprises four aesthetic and environmentally friendly parts that 
includes a golf course, an exclusive gated residential development, boutique 
resort hotel and premiere Country Club in addition to a tract of commercial land 
to house premium office space, retail and entertainment.”<br>
<br>
The first phase of the residential units comprising 80 deluxe villas and 25 
exclusive apartment blocks along the periphery of the golf course are expected 
to be ready by October 2009. The next and final phase of high-value luxury homes 
in nooks around the golf course will be offered by invitation only. He added, 
“Another noteworthy feature at our project will be an Academy, not only to teach 
the game to aspiring youngsters here, but also to train Omanis in the highly 
skilled vocations of golf course maintenance. This will provide high-quality 
employment for citizens not only in Oman but internationally also.” The 
contract, valued at US$110 million, for construction of the villas, apartments 
and infrastructure has been awarded to Larsen &amp; Toubro Oman.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Al Maha Ceramics’ new 
Sohar unit gets operational</b></font><font face="Verdana" size="1"><br>
Al Maha Ceramics Co SAOC, an associate company of Al Anwar Holdings SAOG, 
manufacturing ceramic wall and floor tiles has started the operations of its new 
unit at Sohar. The company is one of the largest producers of wall and floor 
tiles in Oman with a production capacity of up to 5 million sq m tiles per 
annum. The company intends to hold a substantial market share by bringing in 
glazed wall and floor tiles in a variety of designs and shades to suit every 
decor. The tiles are manufactured in keeping with international standards, with 
designs sourced from leading Italian firms. As part of their expansion plans, Al 
Maha Ceramics will appoint dealers around GCC, India and African regions. The 
project is planned in three phases. In the first phase, which is currently under 
production, the installed capacity can produce up to 5 million sq m per annum 
and is completely imported from Italy on a turnkey basis. The facility also 
produces special decorative tiles.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Lulu to invest RO70mn in 
expansion</b></font> <font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>
<img border="0" src="business_briefs5.jpg" width="200" height="156" align="right" hspace="6" vspace="3"></b></font><font face="Verdana" size="1">Lulu, the leading hypermarket and supermarkets chain in the Middle East, is 
expanding its operations in Oman. The new projects include extension of Lulu 
Hypermarket in Baushar to make it a full-fledged shopping mall. The work on this 
has already begun and on completion it is expected to house more than 150 
international brands, food court with more than 30 multi-cuisine outlets, 
amusement center and other world-class features. Apart from this, the group will 
be starting new hypermarkets and mini malls in Salalah, Nizwa, Buraimi and 
Musandam by the end of 2009.<br>
<br>
</font><font face="Verdana" size="1">The chain currently operates six hypermarkets in Baushar, Darsait, Seeb, Barka, 
Sohar and Salalah with a combined floor space of more than 1.2 million sq. ft. 
Yusuffali MA, managing director of the group said, “We have already invested 
more than RO90 million in our existing projects and further RO70 million will be 
invested in the upcoming projects. Another key area that we are focusing is 
Omanization. Out of the 2500 plus staff we currently have, 1340 are Omanis and 
this figure is likely to reach the 2000 mark by the end of 2009,” added 
Yusuffali. According to a company statement, the Lulu retail chain currently has 
69 hypermarkets and supermarkets in the GCC and Yemen enjoying a market share of 
33 per cent in the organized retail sector. By the end of 2009, the group will 
be opening another 12 new shopping malls and hypermarkets thereby further 
strengthening its number one position in the region.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Sabco Media enters into 
FM radio business</b></font><font face="Verdana" size="1"><br>
Sabco Media has launched a new radio station Al-Wisal (means connectivity) that 
promises to “talk less, play more (Arabic music)”. It is targeted at ‘18 to 40 
years, Arabic speaking, modern and stylish, individualistic, adventurous, 
impulsive and interactive listeners’. The new station will focus on infotainment 
by playing music and air chat shows.<br>
<br>
&nbsp;“Any successful radio station has to cater 
to the needs of its listener. Their needs in terms of information, entertainment 
and news about events around town should be met. We hope to present a judicious 
mix of those needs in a very creative, interesting and attractive manner so that 
our listeners get hooked on to our station,” said the company spokesperson. Sabco Media is part of Sabco Group that is into investment, commercial, 
industrial and services related business with international partnerships and is 
chaired by Sayyid Khalid bin Hamad bin Hamoud Al Busaidi. Sabco Media aims to be 
a media solutions provider with specialization in radio and TV operations. The 
company got the license from the Omani government more than a year ago. It has 
plans to launch an English FM radio station also.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Al Omaniya Financial 
Services raise RO10.1mn
<img border="0" src="business_briefs9.jpg" width="200" height="110" align="left" hspace="6" vspace="3"></b></font><font face="Verdana" size="1"><br>
Al Omaniya Financial Services (AOFS) SAOG is giving a dividend of 25 per cent 
for the financial year 2007 (21 per cent cash and 4 per cent stock). Aftab 
Patel, CEO of AOFS informed that the company achieved a profit of RO2.273 
million, a growth of more than 22 per cent over the previous year. Hire Purchase 
assets touched RO85.691 million, a growth of 45 per cent over the previous year. 
The company made a rights issue of 4,550,000 shares at a price of RO2.220 per 
share including 20 Baisas issue expenses, totaling to RO10.1 million. This is 
the largest equity offered by a financial institution in Oman and this will take 
the net worth of the company to RO23 million. Further, the bonds issued last 
year will be converted into shares in July 2009, which will further enhance the 
net worth to RO31 million.<br>
