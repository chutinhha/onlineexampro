            <img border="0" src="cover1.jpg" align="left" width="180" height="230"></font><font face="Verdana" size="1">The 
                    Sultanate of Oman is characterised by political, financial 
                    and monetary stability, free and open economy, vast and 
                    diversified natural resources, especially natural gas, and 
                    important tourism potential. The introduction of appropriate 
                    measures and legislative changes made the economy more 
                    competitive and encouraging to the private sector and 
                    foreign investment. The investment environment characterised 
                    by flexible investment and tax regimes allowing for free 
                    repatriation of capital and profits, modern infrastructures, 
                    industrial zones equipped with the necessary facilities and 
                    infrastructures, created a very conducive environment for 
                    foreign investors. <br>
                    <br>
                    All these factors combined enabled Oman, under the 
                    leadership of His Majesty Sultan Qaboos, to lay the 
                    foundations of a solid economy, to play a distinct regional 
                    role, and to pursue its economic and social development 
                    policies with vigour, confidence and dynamism. The economy 
                    has gradually reduced its level of dependence on the oil 
                    sector. From about 32 per cent share of GDP, a mere 4 per 
                    cent contribution in government revenue and almost less than 
                    1 per cent of our export earnings in 1970, the non-oil 
                    sectors contribute presently to about 59 per cent of GDP, 
                    about 25 per cent of government revenue and about 23 per 
                    cent of the total export proceeds. <br>
                    <br>
                    The country witnessed noticeable economic and social 
                    achievements: a modern physical infrastructure, considerably 
                    improved basic health and education systems and the overall 
                    standards of living of Omanis, and impressive developments 
                    in GDP and GDP per capita. Oman was thus transformed, in 
                    about three decades, from an underdeveloped subsistence 
                    economy to an open, free, modern and dynamic economy. <br>
                    <br>
                    Aware of the vulnerabilities associated with the 
                    overdependence on oil, the government's long-term objectives 
                    were centred on reducing dependency on oil and diversifying 
                    the economic base of the country. Economic policies were, 
                    thus, designed to reshape the role of the government and to 
                    lead Oman on the private-sector-led economic development 
                    path, with a firm commitment to fiscal stability and 
                    sustainability, human resources development and capacity 
                    building, continuation and promotion of the privatisation 
                    process, diversification of the economy, creating employment 
                    opportunities for a fast growing young population, 
                    increasing the level and rate of private savings and 
                    encouraging and promoting private local and foreign 
                    investments. <br>
                    <br>
                    In pursuance of these objectives and policies, the 
                    government has exerted a lot of efforts in the past few 
                    years on various fronts. A major concern was to exploit the 
                    country's important natural gas resources. This has 
                    stimulated the diversification drive based on the 
                    development of gas-based industries and the expansion of the 
                    services sector. Hence, several important major projects 
                    have been completed. These include the container port in 
                    Salalah, majority owned and operated by the private sector, 
                    the LNG plant and the gas pipelines to Sohar and Salalah.
                    <br>
                    <br>
                    On the other hand, despite the adverse developments which 
                    took place in the world during recent years, macroeconomic 
                    performance of the economy has been strong, where it 
                    realised an average of about 5 per cent real growth of GDP 
                    during 1999-2002. In spite of the fall in crude oil 
                    production and the fluctuations of oil prices, total exports 
                    remained stable to about RO44 billion during the past few 
                    years thanks to the increased LNG and non-oil exports 
                    proceeds. <br>
                    <br>
                    The fiscal position is also very good where the overall 
                    fiscal balance averaged about 5 per cent of GDP during 
                    2000-02. The external current account position also remained 
                    firm, the surplus averaging about 13 per cent of GDP during 
                    2000-02. The monetary policy is successfully geared to 
                    realise its objectives: preserving a stable exchange rate, 
                    maintaining price stability and enhancing the soundness of 
                    the financial system. The success of this policy is 
                    reflected in the fact that the real effective exchange rate 
                    of the Omani Riyal remained stable and the most competitive 
                    in the GCC area during most of the recent years, providing a 
                    nominal anchor in the face of unstable oil prices and 
                    serving our major objectives of stabilisation and 
                    development. <br>
                    <br>
                    The soundness of the financial system is reflected in the 
                    well capitalisation and provisioning of the commercial 
                    banks, as well as our known compliance with the 
                    international standards and codes for financial stability.
                    <br>
                    <br>
                    The performance of the economy was, also, very good 
                    vis-à-vis our GCC partners. Oman was among the three GCC 
                    countries with negative average inflation rate during the 
                    period 1998-2002. It has the second best record in terms of 
                    total government gross debt as percentage of GDP, and the 
                    third best record in the real non-hydrocarbon GDP growth, 
                    during the same period.<br>
                    <br>
                    In a nutshell, then, Oman was able to realise the remarkable 
                    achievements and progress outlined above due mainly to two 
                    factors. First, its prudent mix of fiscal and monetary 
                    policies, reflected in the effective management of 
                    government finance, tight yet flexible liquidity policies 
                    and well-managed balance of payment. Second, its commitment 
                    to structural reforms to support the diversification 
                    strategy and the privatisation programs, and to enhance the 
                    competitiveness of the economy and the role of the private 
                    sector.<br>
                    <br>
                    <b>Future Outlook </b><br>
                    The prospects for the Omani economy look good for the year 
                    2004 and the subsequent years, albeit certain difficulties 
                    associated with the decline in oil output in the last two 
                    years and the volatile nature of the oil market. The good 
                    prospects of the economy are based on some objective 
                    factors.<br>
                    The non-hydrocarbon economic growth remains promising over 
                    the medium term, in view of the rising trend of government 
                    and private sector investments in the ongoing and planned 
                    industrial and services projects. Oman LNG is now well 
                    established while QLNG and Oman India Fertiliser Co., in 
                    Qalhat, are now under construction. Such projects will boost 
                    gas usage in the Sur region. The government has invested 
                    heavily in developing the gas fields, pipelines to Sohar and 
                    Salalah.<br>
                    In addition, major new upstream gas projects are being 
                    pursued with the development of a new 20 million cubic metre 
                    per day gas treatment plant at Saih Nihayda. A world-scale 
                    polypropylene project using feedstock from the planned Sohar 
                    Refinery is in development, and the front end engineering 
                    and design phase has been completed. A gas-based 
                    polyethylene project is also being progressed by Oman Oil 
                    Company, with the support of the government. Arrangements 
                    for downstream processing of polyethylene into finished 
                    products are also being considered. The work for an 
                    aluminium smelter plant of a capacity of up to 630,000 
                    tonnes per annum is well underway. <br>
                    <br>
                    The gas sector is, hence, becoming increasingly important to 
                    the economy of Oman and development is proceeding rapidly. 
                    Total gas demand is expected to more than double by 2010, 
                    from the current level of approximately 40 million cubic 
                    metres per day. All these are expected to enable the non-oil 
                    sector to realise a real growth rate of about 6.5 per cent 
                    in 2004 and about 5.5 per cent in subsequent years, bringing 
                    the average real growth of the economy to about 5.5 per cent 
                    in the coming three years. <br>
                    <br>
                    On the other hand, PDO has initiated a major drive to 
                    increase oil output and a series of new enhanced oil 
                    recovery projects are in development. It is hoped that with 
                    these projects, oil production will start to pick up 
                    gradually starting 2004.<br>
                    <br>
                    Furthermore, the government is committed to pursue a prudent 
                    and realistic fiscal policy centred on the strengthening of 
                    fiscal institutions, rationalisation of government 
                    expenditure and enhancing non-oil revenue. It stands ready 
                    to take all the necessary and timely measures to maintain 
                    strong fiscal position. As for 2004 budget, revenues are 
                    expected to be impacted by two factors pertaining to the 
                    decline in oil production, for some technical reasons, and 
                    the fluctuations of the world oil market. All these will be 
                    taken into account for this year's budget. In other words, 
                    it will be guided by prudence in estimating revenues and 
                    rationalisation of expenditure, to keep the fiscal balance 
                    under control.<br>
                    <br>
                    On the other hand, the important steps taken in the past few 
                    years to promote the stability of the financial sector, to 
                    open up the economy for foreign investments and to 
                    strengthen human capital development will be enhanced and 
                    consolidated, to achieve sufficient job opportunities for a 
                    rapidly growing local labour force, and to pursue the 
                    diversification policy. <br>
                    <br>
                    In the regulatory framework, the Sultanate has already made 
                    big strides and laid down flexible investment and tax 
                    regimes to attract foreign investments, allowing for free 
                    repatriation of capital and profits, Furthermore, since the 
                    Sultanate became a member of the World trade Organisation (WTO), 
                    the regulatory and legal frameworks are being adjusted to 
                    conform to the requirements of the WTO and work will be 
                    continued in this direction. <br>
                    <br>
                    Last, but not least is the area of privatisation, where Oman 
                    is known to be the leading country in the Gulf in this 
                    respect. Plans are progressing well and according to 
                    schedule, especially in the areas of electricity and 
                    communications. The important developments in privatisation 
                    initiatives in the past two years included the power, 
                    telecommunication and water sectors, as well as airports, 
                    ports, wastewater projects, postal services and surface 
                    transport. <br>
                    <br>
                    The current status of the power sector (electricity and 
                    water related) is that the government started carrying out 
                    unbundling of the sector and setting up of independent 
                    companies on commercial basis. The process of restructuring 
                    include establishment of electricity holding companies, 
                    independent regulatory body and passing of a new sector law.
                    <br>
                    <br>
                    All the above indicate that future economic prospects of the 
                    economy are very promising. The availability of a modern 
                    infrastructure, constantly upgraded, the very conducive 
                    institutional, legal and regulatory framework makes Oman an 
                    attractive and safe haven for both local and foreign 
                    investments, especially in gas-based industries, information 
                    technology and tourism. <br>
                    <br>
                    Furthermore, Oman enjoys a recognisable international rating 
                    in risk, and is well known for its creditworthiness and 
                    ability to meet its financial obligations. Although the 
                    economy has to deal with a lot of difficulties and 
                    challenges, domestically and internationally, it is yet 
                    pursuing its objectives with determination vigour, 
                    confidence and dynamism.</font></p>
