            <p align="justify"><font face="Verdana" size="4" color="#FF0000">On 
            track off road<br>
                  </font><b><font face="Verdana" size="1"><br>
                  The Jeep Cherokee is a rugged and comfortable 4WD that offers 
            a stable alternative to the urban cute-ute. Vicky Kapur test drives 
            the latest Renegade</font></b><br>
                  <b>
                  <font face="Verdana" size="2"><br>
                  </font>
                  </b>
                  <font face="Verdana" size="1">
            <img border="0" src="car.jpg" align="left" hspace="5" width="300" height="157">The 
            Jeep Cherokee Renegade 2004 is the top-end model within its breed, 
            and is a perfect example of a mean machine that treads the fine line 
            between off-road capability and on-road sophistication. One look at 
            the beast - and the fact that it is a Jeep - is enough to tell you 
            know that the Renegade has some serious fun to offer once you leave 
            the tarmac. This power-packed 4-wheel drive offers a 
            'take-me-anywhere' performance. And that's what separates it from 
            the herd of the so-called compact SUVs, few of which offer true 
            off-road capability. <br>
            <br>
            That said, the Renegade does seem to have sacrificed a bit of 
            refinement and on-road agility to become the perfect off-road 
            companion. On the road, it does not ride or handle as well as some 
            of the other small SUVs. Its relatively slow steering and off-road 
            suspension add up to lethargic transient response in lane-change 
            type manoeuvres. However, the wider tyres of the Renegade offer more 
            stability and make negotiating winding Muscat roads an enjoyable 
            experience. A price-tag of RO9,995 pits it against the Kia Sorento 
            and the Toyota RAV4. The Renegade 4WD is equipped with a standard 
            3.7-litre, V6, 210-horsepower engine. <br>
            <br>
            <b>Exteriors </b><br>
            Its seven-slat grille and round headlights leave no room for doubt 
            that the Renegade is a Jeep. It comes with roof-mounted fog lights 
            and a roof rack, power sunroof, wide wheel fenders and black 
            body-side mouldings, 16&quot; alloy wheels and a cargo cover. The 
            Renegade's body is tall, providing the driver with a commanding view 
            of the terrain ahead. In terms of exterior dimensions, the Renegade 
            fits between the Jeep Wrangler and Grand Cherokee. With an overall 
            length of more than 174 inches, it is longer than Jeep's classic 
            Wrangler, but 7 inches shorter and as much as 500 pounds lighter 
            than the Grand Cherokee. <br>
            <br>
            The Renegade is instantly distinguished by a bold light bar 
            integrated into the leading edge of the roof. It also features a 
            roof basket, bolt-on fender flares, removable side steps, unique 
            graphite-painted alloy wheels, and a body-coloured front fascia. The 
            activity light bar houses two halogen lamps designed to light rocky 
            terrain or a night-time beach party. The Renegade was inspired by 
            Jeep's Dakar concept. It comes in a limited choice of colours: 
            bright cactus green, light khaki, bright silver and black. <br>
            <br>
            <b>Interiors</b> <br>
            The Renegade comes with a roomy interior that can accommodate five 
            passengers and a generous amount of cargo, with good usable space 
            behind the second row of seats. Sitting in the Renegade gives the 
            driver a sense of spaciousness with acres of headroom. Door panels 
            are scalloped out for elbow rests, and a grab handle is provided on 
            the passenger's side of the dash. <br>
            <br>
            Sit in the Renegade, and the first thing you'll likely notice is 
            that it feels tall in the saddle. Its rear seats are comfortable, 
            and there is a lot of rear headroom, and plenty of space to slide 
            your feet under the front seats, but knee room is limited. Getting 
            out of the back seat requires a bit of a stretch down, and your legs 
            drag across the fender. So be sure to clean that area before putting 
            any well-dressed guests back there. <br>
            <br>
            Once you fold the rear seats down, the Renegade offers a lot of 
            cargo space (69 cubic feet). Dropping the split rear seat is a 
            one-hand operation; the rear seat bottom stays in place. Overall, 
            the interior presents a round motif that looks contemporary, with 
            round door handles, round instruments, round air inlets, a round 
            horn pad. Textures and finishes are nicely done. <br>
            <br>
            The accessory controls work well and intuitively. The power window 
            switches are located on the centre console, however, less convenient 
            than if they were on the door. The manually operated 
            heating/air-conditioning controls work well although the mode 
            selector demands attention. The radio works well, but uses a 
            separate and poorly located button to preset stations, an 
            unnecessary distraction when driving. <br>
            <br>
            <b>Performance </b><br>
            Equipped with a standard 3.7-litre, V6 engine and armed with a 
            4-speed automatic transmission with overdrive, the Renegade achieves 
            7 kmpl in the city and 10 kmpl on the highway. If you don't mind it 
            being a gas guzzler, the 3.7-litre V6 works in perfect sync with the 
            automatic transmission, delivering 230 lb ft of torque at 3,800 rpm. 
            The V6 PowerTech engine offers 210hp at 5,200 rpm, allowing the 
            1,866-kg giant a sprint of 0-100 kmph in 11 seconds. <br>
            <br>
            The Renegade comes with Jeep's characteristic Command Trac part-time 
            system. We did shift from 2WD to 4WD on the fly with a slight pull 
            on the hand lever. If you're feeing adventurous, you can take the 
            4WD on wet sand trails without worrying too much of having to 
            forklift one-and-half-tonnes of metal scrap out once the high tide 
            subsides! On really ugly-looking trails, slow down to 4-5 kmph and 
            while still coasting, shift into neutral, and pull the lever up 
            higher for low range. <br>
            <br>
            Like most small SUVs, the Renegade follows the trend away from 
            body-on-frame to unibody construction. This gives it increased 
            strength and rigidity, which allowed the chassis engineers to finely 
            tune the suspension without having to compensate for a Flexible 
            Flyer-type chassis. The Renegade suspension uses coil springs at all 
            four wheels. Breaking from Jeep tradition, the front suspension is 
            independent, with forged steel control arms. <br>
            <br>
            For a better off-road ride, the Renegade offers eight inches of 
            suspension travel. Short front and rear overhangs (the distance from 
            the tire to the bumper) allow steep angles of approach (38 degrees) 
            and departure (32 degrees) in the rough stuff. Progressive-rate 
            springs deliver a nice balance of off-road grip and on-road ride 
            comfort, though humps in the road can be jolting.
