                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="cover4.jpg" align="left" hspace="5" width="180" height="246">Oman’s 
                    banking system has acquired a predominant role in the 
                    development and growth process of the economy. A sound and 
                    resilient banking system has provided a key institutional 
                    mechanism for promoting the aims and aspirations of every 
                    economic agent in Oman, contributing thereby to a sustained 
                    path of progress and achievements. In the process of 
                    acquiring this strategic role in the economy, the banking 
                    system has weathered many challenges, including the fear of 
                    financial contagion from other emerging market economies in 
                    the 1990s, and the impact of large volatility in 
                    international oil prices on aggregate demand. In the 
                    aftermath of the series of financial crises in several 
                    emerging market economies, financial stability has clearly 
                    emerged as a key public policy goal, and in Oman, the 
                    Central Bank of Oman (CBO) can best contribute to the growth 
                    and development process by ensuring a sound monetary and 
                    financial system.<br>
                    <br>
                    The prospects for the banks in Oman, both in terms of future 
                    opportunities and challenges that they may face in 2004 and 
                    beyond, could be better assessed in the context of the 
                    achievements already seen in the past decades of modern 
                    banking in Oman. As against three commercial banks operating 
                    only in the capital area in the early 1970s with a total of 
                    seven branches, now there are five local and nine foreign 
                    banks having a country wide network of about 325 branches. 
                    As against deposit mobilisation of about RO33 million in 
                    1970, the banking system mobilised about RO2,800 million in 
                    2003. Total assets of the banking system rose from RO161 
                    million in 1975 to about RO4,400 million in 2003. In the 
                    1990s, the banking system went through a major process of 
                    consolidation, characterised by mergers, acquisitions and 
                    strategic amalgamations. Adoption of state-of-the-art 
                    technology in banking has been an important feature in Oman, 
                    which helped in increasing speed and reducing costs of 
                    transactions. Today, the banking system of Oman is better 
                    capitalised, and better adapted to the regulatory and 
                    supervisory norms of the CBO, suggesting that there is a 
                    better balance now between growth and stability in the 
                    system as a whole. <br>
                    <br>
                    With a view to assessing the appropriateness of Oman's 
                    regulatory and supervisory framework, Oman voluntarily 
                    participated in a joint IMF-World Bank Financial Sector 
                    Assessment Programme. This external assessment has validated 
                    the relevance of the regulatory and supervisory system put 
                    in place by the CBO. As the challenges and action areas for 
                    the national supervisors are constantly evolving, the 
                    banking system may have to meet the changing requirements of 
                    regulation and supervision as and when they are introduced 
                    by the CBO over time in future. Set against this background, 
                    an assessment of the prospects for Oman's banking system 
                    would suggest that the banks have to prepare themselves for 
                    a number of challenges, including the following:<br>
                    <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">Supporting 
                    and sustaining a process of diversification and 
                    privatisation will be a major challenge for the banks in 
                    future. In the imperative need to reduce the high dependence 
                    of the economy on oil, a pro-active lending policy based on 
                    identification of viable non-oil projects in the private 
                    sector would be critical. <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">The 
                    banking system must be prepared to face the challenges that 
                    may emerge from the economic and monetary union and the 
                    common GCC currency. <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">The 
                    implications of Oman's membership of WTO, particularly in 
                    terms of greater external competition, and the scope for 
                    further strategic collaborations. <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">To 
                    meet the challenges of risk-based supervision, that would 
                    require enhancement in capabilities to identify and measure 
                    various risks to which banks' balance sheets are exposed.
                    <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">Retail 
                    banking would continue to remain important, given the 
                    demographic pattern of Oman which suggests that a large 
                    proportion of the population would experience substantial 
                    growth in consumption and investment demands in future. 
                    Financing small businesses and small and medium enterprises 
                    would help in generating adequate employment opportunities 
                    in the private sector, and the banks have to factor in this 
                    aspect in their future strategies. <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">The 
                    banks also need to respond more to the developments in 
                    international interest rates and exchange rates and 
                    accordingly impart greater flexibility to their interest 
                    rate structures, both on the asset and liability sides. 
                    Pricing of loans need to better reflect sound economic 
                    rationale. <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">Management 
                    of non-performing loans will emerge as a major challenge, as 
                    diversification and privatisation gathers momentum in Oman.
                    <br>
                    </font><font size="1" face="Marlett">8</font><font face="Verdana" size="1">Banks 
                    should also prepare themselves to face the programme to 
                    modernise the payments and settlement systems, particularly 
                    through the introduction of the Real Time Gross Settlement (RTGS) 
                    system and Cheque Imaging. They need to also take note of 
                    the fact that better technology is not meant only for 
                    increasing speed but also for reducing transaction costs.
                    <br>
                    <br>
                    Oman's banking system, thus, has made significant 
                    contributions to the growth and development process of Oman 
                    in the past, but it has to embrace a much greater and more 
                    challenging role in the future. In every market economy, the 
                    link between finance and growth is well established and in 
                    bank dominated financial systems as in Oman, banks can 
                    operate as a key engine of stable economic growth.</font></p>
