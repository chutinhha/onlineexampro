            <font face="Verdana" size="4" color="#FF0000">Duty of Care<br>
                  </font><font face="Verdana" size="1"><b><br>
                  It has always been an inherent responsibility of directors of 
            Omani public companies to serve as honest, vigilant and prudent 
            custodians. Lara Shelton discusses the liability and the duty of 
            care directors of such companies have<br>
            <br>
            </b>The performance of an Omani public joint stock company is 
            primarily driven by the actions of its board of directors. As the 
            guardians of corporate responsibility, it is therefore unsurprising 
            that the law calls the directors of public companies to account when 
            things go wrong. This accountability has recently been brought into 
            focus, especially in relation to the issue of joint and several 
            liability. <br>
            Historically, it was always a matter of legal argument as to whether 
            directors of a public joint stock company could be held jointly and 
            severally liable for corporate wrongdoing (whether civil or 
            criminal). With the promulgation of Ministerial Decision 137/2002 
            (Regulations and Terms of Election of Members of the Board of 
            Directors of Public Joint Stock Companies) ('The Decision'), it is 
            now clear that directors of Omani joint stock companies do have 
            personal joint and several liability for (i) negligent acts or 
            omissions of duty by the Board of Directors which negatively affect 
            shareholder equity; (ii) illegal or criminal actions committed in 
            their capacity as directors and; (iii) acts which exceed their legal 
            scope of authority or their company's articles of association or 
            by-laws. <br>
            Directors, particularly when first elected to a board, may be 
            concerned about the possibility that they will be held personally 
            liable for losses the company suffers as a consequence of the 
            Board's decisions. They frequently ask what they must do to avoid 
            such liability. The answer to that question often begins with a 
            discussion of the director's duty of care, which is a duty based on 
            the idea that the relationship of a director to the public company 
            he serves is that of a fiduciary. <br>
            The duty of care is the obligation of a director to act in good 
            faith, to the standard of a reasonable and prudent person in similar 
            circumstances, and in a manner that he reasonably believes is in the 
            best interests of the company. This duty is inherent in Article 109 
            of the Oman Commercial Companies Law, which broadly provides that 
            directors will face liability if they fail to 'act as prudent men 
            under the circumstances.' The purpose of the duty of care is 
            therefore to ensure that directors exercise their judgement in good 
            faith and in the normal course of business. It is designed as a 
            constraint to safeguard against corporate wrongdoing.<br>
            Observance of the duty of care generally requires that a director 
            should acquire, at the very least, a basic understanding of the 
            business of the company he serves. The obligation on directors to 
            exercise this duty of care means they cannot defend any failure to 
            discharge it on the basis that they lacked the knowledge needed to 
            exercise the expected degree of care. If a director feels that he 
            does not have sufficient business experience to qualify him to 
            perform the duties of a director, he should either acquire the 
            knowledge before accepting the position, or refuse to act as a 
            director. Additionally, directors are under a continuing duty to 
            keep themselves informed about the activities of the company. 
            Directors may not simply close their eyes to corporate misconduct 
            and then claim that because they did not see the misconduct, they 
            did not have a duty to look.<br>
            Discharging this duty of care does not require a detailed 
            surveillance of day-to-day activities, but rather a general and 
            careful monitoring of corporate affairs and policies. Accordingly, 
            directors are well advised to attend board meetings regularly. 
            Regular attendance does not mean that directors must attend every 
            meeting, but that they should attend as many meetings as are 
            practicable. Similarly, while directors are not required to audit 
            corporate books themselves, they should maintain close familiarity 
            with the financial status of the corporation by a regular review of 
            financial statements. The extent of the review, as well as the 
            nature and frequency of financial statements, depends not only on 
            the customs of the industry, but also on the nature of the company 
            and the business in which it is engaged. <br>
            In a public company, where directors delegate the on-going operation 
            of the business to management, part of the director's monitoring 
            function involves the subsequent oversight of those to whom the 
            delegation has been made. How much, and under what circumstances, 
            such oversight is required is a question which directors must 
            confront on a continuing basis. Generally, directors may rely on the 
            honesty and prudence of management and others in a subordinate 
            position until something occurs to put them on suspicion that 
            something is wrong.<br>
            If such an occurrence goes unheeded, the directors expose themselves 
            to liability for failing to discharge their duty of care; but in the 
            absence of any cause for suspicion, there is no duty upon the board 
            to conduct a day to day surveillance and investigation of the 
            company's operations. <br>
            One aspect of the directors' general duty of care obligation in the 
            'oversight and enquiry' area is an affirmative obligation to be 
            reasonably concerned about the existence and effectiveness of 
            procedures, programmes and other systems to assist the board in 
            overseeing the company's business. The Decision recognises the 
            company's general compliance with law as one of the core functions 
            of the board, and introduces an affirmative duty to implement a 
            compliance programme to emphasise the need for policies and the 
            implementation of procedures to ensure legal compliance by the 
            company with the law. For example, the Decision requires that the 
            'Board of Directors shall undertake...the disclosure procedures 
            pertaining to the company and follow up their implementation in 
            accordance with the rules and conditions of disclosure issued by the 
            Capital Market Public Authority...[and] the assessment of the 
            performance of the staff...and evaluation of the work carried out by 
            the committees formed by the Board of Directors according to...the 
            Commercial Companies Law. '<br>
            Although the Decision clarifies that directors do have joint and 
            several liability for civil and criminal wrongdoing, as well as for 
            those actions which exceed their authority, it is nevertheless still 
            recognised that directors are not insurers of the companies they 
            serve. Business is inherently risky, but directors will invariably 
            have satisfied their duty of care if they make business decisions 
            based on prudent, intelligent judgements made in good faith, even if 
            those judgements do not produce the intended result for the company 
            and the shareholders' equity. <br>
            Lara Shelton is an Attorney with Denton Wilde Sapte, Muscat <br>
