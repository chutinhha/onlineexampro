            <font face="Verdana" size="4" color="#FF0000">Safe yet stylish<br>
            <br>
            </font><b><font face="Verdana" size="1">The</font></b><font face="Verdana" size="1"><b> 
            Volvo S80 is a classy car offering oodles of comfort. Although its 
            safety features remain paramount, ignoring its luxury simply is no 
            longer an option writes Nayl D'Souza</b><br>
            <br>
            <img border="0" src="car.jpg" align="left" hspace="3" vspace="3" width="300" height="144">Last month, some plaques in a Muscat-based four-star hotel contained 
            a very useful adage for its guests. The anonymous saying went: &quot;Do 
            not undermine your worth by comparing yourself to others. It is 
            because we are different that each of us is special.&quot;<br>
            Applying this maxim to a few brands in the luxury saloon segment 
            seems highly appropriate; especially to Volvo. This is despite the 
            fact the Swedish car manufacturer - which was acquired in 1999 by 
            Ford Motor - has established a brand that has become synonymous with 
            safety. <br>
            Apparently, in recent times, Volvo has made a deliberate effort to 
            move away from its former reputation as simply a producer of safe 
            cars popular with college professors and suburban housewives. Alas, 
            it seems that without a trendy image many people (rightly or 
            wrongly) simply overlooked the Volvo showroom because the cars on 
            display there were not chic enough for them. <br>
            If Volvo were allowed to add a three-pointed star to its bonnet, or 
            don four interlocking circles to its rear bumper, or even wear the 
            blue and white roundel emblazon with the letters BMW, would it do 
            so? No, it wouldn't. By adding curves to its cars that earlier had a 
            distinctively boxy shape and by rolling out smooth new models like 
            the S80, Volvo has already begun driving forward in a more rewarding 
            direction. This is a consolation that more than makes up for it 
            being less of an extrovert than some of its showy adversaries.<br>
            So, in spite of the adage in the hotel, OER took the Volvo S80 2.9 
            Litre Executive (2003) model out for a road test in Muscat to 
            discover whether the Volvo could underline its worth and prove it 
            has more than enough verve to offset its competitors.<br>
            <br>
            <b>Price</b><br>
            In Oman, the car's main rivals are the BMW 530 (2.9 litre), Audi A6 
            (3.0 litre) and the Mercedes E240 (2.6 litre). The S80 2.9 Litre 
            Executive is priced convincingly well for a mid-level luxury sedan 
            at RO16,000. This is, at today's advertised prices in the local 
            market, far less than the Beamer, slightly less than the Merc and 
            somewhere in the range of the Audi. <br>
            <br>
            <b>Outdoors<br>
            </b>The Volvo S80 has a moderately sophisticated stylish exterior 
            that feels more subtle than quick. It certainly hints at the 
            exhilaration, comfort and opulence which are hidden from view inside 
            the cabin and under the bonnet. Sure, the front fascia is slick but 
            aren't most cars these days? The bi-xenon lamps are appreciated 
            also. But even though the tedious angular curvatures from the past 
            have been ironed out, the car's appearance could be bolder. <br>
            The 2.9 Litre Executive model has an electronic glass sunroof fitted 
            with vent mode as standard plus laminated side windows and a 
            reflective glass windscreen. <br>
            <br>
            <b>Indoors <br>
            </b>Once inside the Volvo S80 using keyless entry, owners are able 
            to adore its many extraordinary and unique differences. The interior 
            is well designed and very pleasing. Indeed, the electronically 
            foldable head restraints on the outer rear seats set this car apart 
            from its contemporaries by giving the driver a convenient way to 
            improve rear vision when reversing at the touch of a button. <br>
            Leather seats and wood trim are expected in this class of car, and 
            Volvo adds more than a nice touch with its use of these materials. 
            As standard, the S80 2.9 Litre Executive is fitted with luxurious 
            leather upholstery, plenty of storage and cupholders for all 
            occupants, illuminated vanity mirrors for driver and co-passenger, 
            and electronic climate control. A trip computer helps drivers plan 
            their journey more economically showing current and average fuel 
            consumption.<br>
            For the driver, there is a power seat with memory for three 
            different drivers/positions, cruise control and remote control 
            volume for the music system and cell phone controls on the steering 
            wheel. The S80 allows you to use your SIM card and make hands free 
            calls. An integrated cell phone keypad is located in the centre 
            console while a microphone and speaker are integrated into the 
            rear-view mirror. <br>
            The front seats are extremely comfortable (if not perfect) and in 
            the front are electronically-adjustable. <br>
            Rear-seat occupants have more room in the S80 than most of its 
            competing luxury brands. This is aided by the rear of the front 
            seats being indented to allow extra knee and foot space. 
            Specifically for rear passengers, the air conditioning blows across 
            into their faces from air ducts positioned on their side window 
            partition rather than upwards from behind the centre armrest. This 
            is a great feature to avoid restlessness when the journeys are long.
            <br>
            The S80 OER test drove had a 200-watt Dolby Pro Logic stereo with a 
            four-disc in-dash CD player fitted as standard. It rumbled along 
            nicely to some modern pop delights.<br>
            The trunk isn't enormous but it's shaped in such a way that if the 
            rear seats were folded forwards fully, you could fit four medium 
            suitcases with ease (as was tested).<br>
            It is worth mentioning here that Volvo also produces a 
            top-of-the-line S80, a twin-turbo T6. Priced at RO18,000, the car 
            maker has gone to the trouble of installing a gorgeous entertainment 
            console for the benefit of rear passengers. It has also fitted a 
            built-in refrigerator and a storage compartment with four hand-cut 
            glasses made of finest Swedish crystal for good measure. <br>
            <br>
            <b>Performance</b><br>
            A six-cylinder, 24-valve engine powers the S80 2.9 litre Executive 
            model. It delivers a sweet top speed of 210 km/h. <br>
            The car's brakes are very responsive, progressive and confidence 
            inspiring. This is certainly needed because you will not get left 
            behind in traffic driving this car. The S80 accelerates from nought 
            to 100 km/h in 8.9 seconds without any strain and little roar. 
            Cruising is effortless, relaxed and quiet - and you consume 10.8 
            litres of fuel per 100 km. Here, it is again worth bringing up the 
            top-range S80 T6 which, in comparison, has a top speed of 250 km/h, 
            can move from nought to 100 km/h in only 7.2 seconds and guzzles 
            11.3 litres of fuel per 100 km.<br>
            When cornering, the torque of the less-charged S80 2.9 litre is 
            impressive and the car remains stable when neutrally steered or when 
            given sudden directional contortions. It is a very quiet car to 
            drive which is again great news for those long distance drives. <br>
            The torque rod, which is a standard feature attached to the engine 
            of the S80, makes the car sportier and provides a far more 
            comfortable ride. Hooked up to either side of the power train, it 
            soaks up the vibration and noise from the engine and improves the 
            car's overall stability and traction control. <br>
            <br>
            <b>Safety </b><br>
            On the safety front, Volvo continues to emphasise the trait that 
            originally brought it to prominence. The S80 has six dual stage 
            front and side air bags for front-seat passengers including two 
            inflatable air-curtains with added head injury protection for both 
            front and rear passengers. The vehicle has head restraints for every 
            passenger, and specially engineered seats up front to help prevent 
            whiplash. Also, a rain sensor activates the windscreen wiper 
            electronically as soon as water splashes onto the windscreens.<br>
            Volvo invests a terrific amount of time and capital developing 
            optimum protection for occupants of its vehicles in the event of a 
            fender-bender. Volvo made the world's first official robotic 
            pregnant crash dummy which studies how the seat belt and airbag in 
            simulated accidents affects the mother-to-be and her unborn baby and 
            has its own Traffic Accident Research Team which studies road 
            accidents. <br>
            <br>
            <b>2004 Outlook </b><br>
            Volvo has already showcased its 2004 model S80 AWD (all wheel 
            drive). Although not much fine-tuning was required this model, of 
            course, will be welcomed nonetheless. All-wheel drive S80s are 
            expected to provide better acceleration on all road surfaces, 
            combined with more stable road performance. Sadly, it will not be 
            seen in great numbers in the Gulf for a short while as it is not 
            part of the standard product portfolio for the GCC.<br>
