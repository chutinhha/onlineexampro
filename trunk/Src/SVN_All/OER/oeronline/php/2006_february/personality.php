<p align="justify"><font face="Verdana" size="3"><b>
<img border="1" src="pers.jpg" width="200" height="167" align="left" hspace="5" vspace="2">T</b></font><font face="Verdana" size="1">ariq 
Ali Al Ajmi could have joined his father’s 30-year old business right after 
completing his studies. But he did not. He preferred to gain experience 
elsewhere before returning to the homestead. A graduate with Honours in 
Electronics and Electrical Engineering from Glasgow University in Scotland and 
Masters in Computer Engineering from Sultan Qaboos University, Tariq decided to 
begin his career with Omantel where he spent three solid years before taking 
charge of Civil Contracting Co LLC (Civilco) as Managing Director.<br>
<br>
“I think that’s the best way of learning for a fresher because you are not 
familiar with the atmosphere. I felt I had a lot of learning to do before 
joining the family business, avers he. He joined Omantel in the Engineering 
Division as Planning and Implementation Engineer.<br>
<br>
<b>Learning the ropes</b><br>
By the time he had completed three years, his father asked whether he would like 
to join the family business, and the young Tariq had no qualms in accepting the 
offer. “I felt that it was time I got round to helping my father,” he says. 
Running a business was no piece of cake. He was scared initially, no doubt, but 
accepted it as a challenge.<br>
<br>
Though his father helped him a lot, Tariq understood the need to learn the ropes 
to run the business well. Very quickly, he learnt that one had to have a 
thorough understanding of key areas such as finance, management and 
implementation processes. Sensing that engineering background will not suffice, 
Tariq is currently studying for his Masters in Business Administration, 
part-time with Strathclyede University to shore up his skill sets.<br>
<br>
<b>Business talk</b><br>
Two years after taking charge, he is chiseling out a new working strategy for 
the company. “I felt we could do more to enhance our working styles and 
workforce,” adds Tariq. He hopes to implement the plan soon. Of course, he is 
not ready yet to share details, except claiming that he would be able to 
withstand competition in the marketplace well. <br>
<br>
Civilco is currently involved in electro-mechanical, civil engineering, 
mechanical, and landscape projects. The setting up of sub-stations and 
transmission lines in Rustaq and for Petroleum Development Office (PDO) are some 
of the major projects that the company has bagged recently. <br>
<br>
Under the new strategy, Tariq hopes to venture into new grounds very soon. <br>
<br>
“One field that we have not taken up is projects related to roads. Soon we hope 
to enter this field,” says the young entrepreneur. His vision for the company is 
to grow and diversify into different fields.<br>
<br>
<b>Taking time out</b><br>
Tariq is so consumed by his work that by the time he gets back home he usually 
is deadbeat. That does not stop him from giving into his passion for computers. 
“I like to assemble a computer by myself. There are times when I dismantle a 
computer to know all about how it works. I have learned how to handle a lot of 
technical problems related to computers through this passion.” No wonder most of 
his folks call him when having problems with their computers.<br>
<br>
Computer games also get Tariq excited. “I also have the whole collection of 
Japanese animation video game Manga series, which I usually purchase online,” he 
says. Sports and gym are his other interests, but he is rarely able to pursue 
it. “I am usually very tired when I get home after a hard day’s work and 
attending MBA classes. I am quite enthusiastic about sports, but when I come 
back from work I prefer to relax,” he says with a grin. - Anne Kurian</font></p>

