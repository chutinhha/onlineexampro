							<p align="justify" style="margin: 10px">
							<font face="Verdana" size="1">For decades oil wealth 
							was sufficient in itself to sustain a standard of 
							living that, in some cases, was the envy of the 
							world. Today, Arab oil producing countries like many 
							others grappling with the challenges of the current 
							global knowledge economy are at a crossroads. Since 
							the discovery of oil, internal and external factors 
							have conspired to transform their position from one 
							of comfortable complacency to one of real concern 
							for the future. The Gulf is no longer insulated from 
							the unrelenting effects of globalisation or, as 
							graphically expressed by Schumpeter in an earlier 
							context, from “the gales of creative destruction.” 
							Globalisation does not respect national boundaries 
							and, like it or not, developing and developed 
							nations are now part of the so- called ‘global 
							village’.<br>
							<b>Rodenbeck identified some of the more pressing 
							internal problems:</b><br>
							• The growing realization that oil and natural gas 
							have a limited lifetime; at present production 
							levels, Oman’s known oil reserves will last 17 
							years. Bahrain, 10 years, Saudi Arabia 100 years, 
							Kuwait 126 years and the UAE, 150 years. <br>
							• The Gulf region has the fastest growing population 
							in the world. By the year 2000, the growth rate for 
							the Gulf Cooperation Council (GCC) nations (Oman, 
							the UAE, the Kingdom of Saudi Arabia, Qatar, Bahrain 
							and Kuwait) had reached 2.75 times the world 
							average, which, if sustained, would lead to a 
							doubling of the Omani national workforce by the year 
							2020. Currently, 60 per cent of Omani nationals are 
							under 25 years of age.<br>
							• The combined income of GCC nations is roughly 
							comparable to that of Switzerland while the GCC’s 
							population of 30.4 million is more than four times 
							as big and it has been increasing at nine times the 
							Swiss rate over the last 25 years.<br>
							• Merchandise imports into the GCC are at about the 
							same level as the combined imports into India and 
							Russia.<br>
							• Oil export earnings in the GCC decreased from $150 
							billion in 1980 to an average of $80 billion in the 
							1990s.<br>
							• The 11 million expatriate workers in the region 
							repatriate about $25 billion in remittances each 
							year or 10 per cent of the GDP of the Gulf States.</font></td>
