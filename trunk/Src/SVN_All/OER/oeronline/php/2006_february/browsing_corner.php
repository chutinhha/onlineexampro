            A Good Buy</font></b><font face="Verdana" size="1"><br>
            <br>
            <img border="1" src="br_corn.jpg" width="150" height="226" align="right" hspace="5"></font><font face="Verdana" size="3"><b>O</b></font><font face="Verdana" size="1">man 
			is known the world over for its rich culture and tradition. In their 
			book on Oman, authors Vincent McBrierty, an academician and 
			consultant, and HE Mohammad Al Zubair, trace Oman’s history from as 
			far back as 4000 BC to the present times and examine the measures 
			undertaken by the country to march towards a knowledge-based 
			economy. They outline the opportunities Oman has and the challenges 
			it faces. <br>
			<br>
			The authors trace Oman’s transition from a primarily trading economy 
			to a knowledge-based economy. For this, development of human capital 
			holds the key. Says the book: “Developments have profoundly altered 
			the way in which nations go about their business. They can no longer 
			function in isolation from the rest of the world; they are no longer 
			fully in control of their future, particularly in the area of social 
			and economic growth.” The authors thus sum up the changing face of 
			the global industry. <br>
			<br>
			The book narrates the history of the land from 4000 BC to 1750 AD. 
			The third millennium BC was a golden period for Oman, known among 
			its parallel civilizations as ‘Magan’. The second millennium BC saw 
			a decline in prosperity and affluence but from the first millennium 
			BC onwards, Oman’s enviable strategic location re-established its 
			domination of trading sea routes. <br>
			<br>
			The book describes how trade suffered a setback in the third Century 
			AD and how during the sixth Century AD the Sassanids established 
			their bases in Northern Oman until the seventh Century when the Al 
			Azd tribes, under the Julandas, finally drove the Persians out. The 
			coming of Islam and its adoption by Oman in 630 AD brought further 
			changes and enhanced opportunities. The year 1507 saw the Portugese 
			holding forte, which continued till the 1650 AD when the Al Ya’ribah 
			dynasty finally expelled them. Persians took over for a brief period 
			around 1737 AD, only to be forced out by the Al Bu Said dynasty. <br>
			<br>
			The book also delves on the Al Bu Said dynasty’s succession from 
			1744 when Imam Ahmed bin Said married the daughter of the last Al 
			Ya’ribah ruler. The trials and tribulations of all his descendants 
			are briefly recorded and summed up well with a speech of the current 
			ruler, His Majesty Sultan Qaboos bin Said, where he says of running 
			a country well: “It’s an art. Either you’ve got the touch to do it 
			or you don’t.” <br>
			<br>
			From the writings of the traveler Ibn Battuta to archaeological 
			findings, and from research at the Sultan Qaboos University to the 
			Government statistics, the authors have a formidable pool of 
			information on agriculture, fisheries, aromatics and perfumes, 
			copper mining, processing, boat building and cottage industries. All 
			sectors of the economy are well researched and make excellent 
			reading to understand the economy’s progression. Islam’s role in 
			building and strengthening the knowledge economy is outlined well.
			<br>
			<br>
			The authors also analyses the global experience of economic growth, 
			outlining how successful the US, India and China have been due to 
			their knowledge-driven economies, albeit through different 
			circumstances. The Russian Federation, on the other hand, lacked the 
			necessary infrastructure to support its vibrant knowledge economy, 
			say the authors. The UK made such progress as late as in 1998 and 
			the authors suggest that a holistic and comprehensive approach ought 
			to be used. <br>
			<br>
			The authors analyse how the small and open economies of Botswana and 
			Norway have fared despite being rich in resources (they have 
			diamonds and oil). And how Singapore and Ireland have done without 
			any of these advantages!<br>
			The national education system of Oman and the role of the 
			educational institutions in graduate training, research as well as 
			economic growth are listed well. <br>
			<br>
			The Sultan Qaboos University as a source of human resource 
			development and a center for research and consultancy are examined 
			well by the authors who were both associated with it. No student of 
			Omani economy should overlook the chapter on economic growth since 
			1970. The authors have candidly recorded some facts and figures of 
			the global oil industry in general and of Oman’s in particular. 
			Oman’s infrastructure, private sector and the barriers to policy 
			formulation figure in the book. The six five year plans are 
			described in details, complemented with GDP statistics. <br>
			<br>
			The authors showcase Oman’s tourism potential well, explaining the 
			significance of its caves, flora and fauna, forts and castles. The 
			huge potential of the sea front development projects and resorts are 
			also explored. <br>
			<br>
			The concluding analysis and the appendix on Islamic banking make 
			interesting reading. Overall, a book full of statistics and graphs, 
			and certainly not meant for casual readers! Highly recommended for 
			anyone interested in Oman’s economy and history. - Uma Devi Jadhav</font></p>
