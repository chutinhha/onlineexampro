<b>The Greek Recipe</b></font><font face="Verdana" size="1"><br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="pass_thr.jpg" width="100" height="119" align="left" hspace="5" vspace="2">P</b></font><font face="Verdana" size="1">rofessor 
Panayotis Alexakis, who was on a visit to Muscat recently, shares the experience 
of the Greek capital market. Alexakis is a financial economist, Professor of 
Economics at the University of Athens, an advisor to the Investment Bank of 
Greece and, last but not the least, is a former President of the Athens Stock 
Exchange As preparation for our entry into the Euro Zone - Greece entered the 
Euro Zone on 1 January 2002 - we had to restructure our economy. The government 
did a good job of this: it reduced interest rates and given the reverse 
relationship between interest rates and equity markets, the latter started 
moving up. In 1999, we had a big rise in the index and then the bubble burst - 
as the rise was beyond the level warranted by normal market conditions. In fact, 
this is what happened to most of the European markets. Between 2000 and 2001, 
the market continued to fall, but when Greece thought that the market had 
bottomed out, the world was hit by 9/11 and there was subsequent loss of 
confidence and the impact of the falling American economy. In 2002, the market 
started going up. Now, it still maintains its upward trend (Currently, the 
Athens stock exchange’s market capitalisation is ten times of MSM). <br>
<b><br>
Scandals, Scams &amp; Regulatory Framework</b><br>
We too have experienced financial scams, but it has not been associated with 
very large companies. Therefore, these scandals did not receive much publicity. 
Since the Greek market is truly international, with almost 40 per cent of 
foreign investment, we set very high standards of corporate governance, 
transparency and disclosure.<br>
<br>
The good thing about Greece is that we have to follow the European Union’s 
procedures and basic regulations in terms of listing of companies, disclosures, 
information dissemination, requirements of listed companies, and brokerage 
houses etc. As of 2005, the whole of Europe, including Greece, compulsorily 
requires compliance with International Financial Reporting Standards (IFRS) for 
all listed companies and that has added to the transparency of financial 
information. The Management has to certify the financial statements. Though we 
have not implemented the Sarbanes Oxley Act as a whole, we have definitely 
become stricter in recent times.<br>
<br>
The Capital Market Commission of Greece (CMCG) has a separate department for 
auditing the listed companies. It conducts regular and ad-hoc audits. Besides 
CMCG, we also have the luxury of the Commission for Accounting Standardization 
and Control, set up in 2003. It has two sub-organizations: while one deals with 
the standardization of accounting issues, the other deals with control issues.
<br>
<br>
The control arm of this body picks a random sample of 10 per cent of the listed 
companies every year and scrutinizes them, evaluates whether their internal and 
external audits have been carried out satisfactorily and if required, further 
examination is done. This is a very good innovation and I don’t think that any 
other country in Europe has a similar model.<br>
<b><br>
Investor Psyche</b><br>
After the 2000 downturn, when retail investors lost a lot of money, they 
preferred to stay away. Now, foreign investors are active and all major 
investment companies of the world have invested in Greece. Slowly, retail 
investors have returned too, but they are more cautious now. We too have done a 
lot of work to educate the investors. Advertisements and articles in the local 
press were published urging investors to do their homework before making 
investments and not to be influenced by rumours and word to mouth notions. <br>
<br>
Adversaries were posted asking them to look at the fundamentals and balance 
sheets and check financial strengths of the companies they choose to invest in. 
We did a lot of work and it took a long time but I think we managed to get 
investors to learn about sensible and responsible investing. Most retail 
investors like to invest in Greece. Rather than investible funds going out of 
Greece, we see more of foreign investment coming into Greece.<br>
<b><br>
Market Challenges</b><br>
The greatest challenge, in my opinion, is to ensure that the markets remain 
liquid because liquidity ensures growth and to maintain liquidity, you need to 
have companies with dynamism, profitability, transparency and integrity. 
Moreover, I would like to see more emphasis on corporate governance. For me, 
corporate governance is not just about compliance with regulations. Nor is it 
just to have independent members on the Board. It is a culture. It can start 
with rules and regulations and codes of conduct but eventually it has to become 
a culture. This culture should seep into the very fabric of the companies for 
good corporate governance to be exercised in its true spirit.- As told to Afsana 
Patel</font></p>

