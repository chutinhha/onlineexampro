                              <b>PEIE wraps up Smart Man Oman<br>
                              </b>The Smart Manufacturing Conference, held 
                              during 23 – 24 January at the Muscat 
                              Inter-Continental Hotel was a resounding success. 
                              It was organized by Public Establishment for 
                              Industrial Estates (PEIE). Mohammed Al Ghassani, 
                              Executive Vice President, PEIE said: “Given 
                              initial attendee feedback, the conference has to 
                              be described as an outstanding success. The seven 
                              sessions that made up the two day program plus the 
                              Lean Enterprise Workshop held on the 25th at 
                              Knowledge Oasis Muscat all went extremely well and 
                              we’re delighted with the outcome and the momentum 
                              the event has generated.” <br>
                              <br>
                              In delivering the Conference closing remarks, Al 
                              Ghassani said: “Oman’s Manufacturing is a success 
                              story – we excel in ceramics; precision 
                              engineering; furniture; fibre optics; batteries; 
                              textiles through to food processing. Indeed, 
                              manufacturing is a major contributor to Oman’s 
                              national wealth. It accounts for a major slice of 
                              our exports and provides thousands of jobs and 
                              supports a flourishing service sector. Our best 
                              manufacturers and best products are world class 
                              and sought after in the most highly competitive 
                              markets around the globe.”<br>
                              <br>
                              However, he admitted that manufacturers also face 
                              huge challenges. As discussed at the conference, 
                              the future of Oman’s manufacturing depends on 
                              raising investment, applying innovation and 
                              technology, smarter marketing, design and 
                              packaging, best practice and skills to create even 
                              better products that the world wants to buy. <br>
                              <br>
                              To ensure that the momentum created by the 
                              Conference is maintained, Al Ghassani announced 
                              PEIE’s intention to establish a Manufacturing 
                              Discussion Group – a group that would aim to 
                              stimulate ideas and discussion in priority areas 
                              such as skills development, marketing, design, 
                              links between industry and academia, technology, 
                              logistics, new markets and the image of 
                              manufacturing. <br>
                              <br>
                              “No one would claim that the way ahead for Oman’s 
                              manufacturing is easy. Recent failures highlight 
                              some of the difficulties. But they are only a tiny 
                              fraction of the overall equation. Manufacturing 
                              commitment to high value and skills, together with 
                              extensive practical government support and 
                              maintenance of the right conditions for business 
                              to flourish will enable Oman-based manufacturers 
                              to compete in the long-term. And that’s good news 
                              for everyone,” concluded Al Ghassani.</font></td>
