                    <font face="Verdana" size="1">
                              <b>NBO declares record net profit</b><br>
                    National Bank of Oman SAOG has bounced back with a net 
                    profit of RO 20.3 million in 2005, the highest in NBO’s 
                    history, as against a profit of RO 5.2 million reported for 
                    the year ended 31 December 2004. After a break of four 
                    years, NBO has resumed dividend payment by declaring a cash 
                    dividend of 15 per cent. <br>
                    <br>
                    Although there was an increasing interest rate scenario in 
                    2005, the bank has been able to register an increase in net 
                    interest margin as compared to 2004. The bank has been 
                    successful in overcoming its past challenges with regard to 
                    the large level of non-performing loans (NPLs) and its 
                    resultant impact on the profitability of the bank. <br>
                    <br>
                    The 
                    position has stabilized in 2005 with lower loan loss 
                    provisions of RO 17 million as compared to RO 31 million in 
                    2004 and higher recoveries from provisions of RO 20 million 
                    as compared to RO 18 million in 2004. Also the level of 
                    NPLs’ to gross loans was lower at 15 per cent as compared to 
                    31 per cent and the provision coverage has further improved 
                    to 84 per cent in 2005 as compared to 81 per cent in 2004.
                    <br>
                    <br>
                    NBO’s CEO Andrew Duff added, “The bank is focusing on 
                    qualitative aspects as well. Omanisation is currently at 
                    92.5 per cent with all branches continuing to be fully 
                    manned by Omani staff. In addition, steps to increase 
                    motivation and performance of the staff are being taken, 
                    including a proposal for a 5-day work week for staff with 
                    the bank remaining open for six days.”</font></td>
