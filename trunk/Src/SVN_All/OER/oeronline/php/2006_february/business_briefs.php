                    <font face="Verdana" size="1">
                              <b>Muscat Index Fund renewal</b><br>
                              Muscat Index Fund (JIA), the Sultanate’s first 
                              index fund, managed by BankMuscat’s Asset 
                              Management Division (AMD) recently held its Extra 
                              Ordinary General Meeting (EGM) to place before 
                              unitholders proposals on the renewal of the fund. 
                              The JIA was launched in November 1994 for a 
                              ten-year period.<br>
                              <br>
                              The proposals of the investors’ committee to renew 
                              the fund under new terms and conditions were 
                              warmly received by the unitholders. Under the new 
                              scheme, JIA will be changed from its erstwhile 
                              closed-ended structure to an open-ended structure; 
                              the fund would offer a weekly 
                              subscription/redemption facility; the fund would 
                              be listed on the Muscat Securities Market; and 
                              would have the flexibility to invest in non-index 
                              stocks up to a limit of 20 per cent of the fund 
                              size with an objective to improve overall returns. 
                              The Fund would also be renamed as the Muscat Fund. </font>
