            <p align="justify"><br>
			<font face="Verdana" size="2"><b>Seventh 5-Year 
			Plan (2006-2010)</b></font><font face="Verdana" size="1"><br>
			</font><b><font face="Verdana" size="5">Creating the platform for 
			future growth</font></b><font face="Verdana" size="1"><br>
			<br>
			</font><font face="Verdana" size="3"><b>
			<img border="0" src="cover1.jpg" width="300" height="145" align="left" hspace="5" vspace="2">T</b></font><font face="Verdana" size="1">he 
			economists had expected further liberalization and development at an 
			accelerated pace whereas the citizens of Oman expected the 
			government to raise their standard of living from the Seventh 5-Year 
			Plan 2006-2010. It satisfies both. <br>
			<br>
			While presenting the plan, Ahmed bin Abdulnabi Macki, Minister of 
			National Economy and Deputy Chairman of Financial Affairs and Energy 
			Resources Council, had outlined its objectives as: “Improvement of 
			the standard of living of the citizens; maintenance of stability of 
			prices; upgrading education and expanding the higher education 
			opportunities; to strive to provide employment opportunities for the 
			nationals; awarding attention to the issues of population, social 
			welfare, sustainable human development, infrastructure and the 
			regional dimension of the development; enhancing the water resources 
			and provision of safe water; preserving national heritage; 
			strengthening the capabilities of the judicial system and upgrading 
			its performance level; development of tourism, fisheries, industry 
			sectors and encouragement of export processes; and encouragement of 
			local and foreign private sector investments.”<br>
			<br>
			<b>Growing Confidence</b><br>
			The announcement of the Seventh Plan showcases the growing 
			confidence of Oman. The Sultanate, brushing aside its conservative 
			fiscal planning image, has planned a major diversification exercise 
			with a major role for the private sector in the next five-year 
			period. For the first time in the five-year plans, the deficit is 
			going to be as high as RO 2260 million (12 per cent of the 
			government expenditure) during the period. The deficit will be 
			financed from borrowing and withdrawal from the reserves. The 
			government has also given equal emphasis on the social development 
			of Omanis in the process of economic development.<br>
			<br>
			The government realises that the public sector alone cannot meet its 
			above objectives, which are part of the broader Vision 2020 
			programme. The private sector has to play a larger role than ever 
			before. No wonder private sector is expected to contribute 46 per 
			cent of the total investment of RO 13.1 billion planned for 
			2006-2010. The rest 54 per cent comes from the public sector. <br>
			<br>
			<b>Higher growth possible</b><br>
			At current prices, Oman’s economy is expected to grow at an annual 
			average rate of three per cent. Going by the trend in the past, the 
			three per cent figure seems on the lower side. “In the Sixth Plan, 
			Oman had forecast a GDP growth of 0.6 per cent (annual rate at 
			current prices) but ended up with a growth rate of 8.7 per cent. The 
			Seventh Plan is based on the presumption that the oil price is going 
			to stick to an average of US$30 per barrel in the next five years. 
			The market foresees the oil price going higher than US$30, which 
			implies that Oman’s GDP growth (relying heavily on oil revenues) is 
			going to be higher than the planned rate,” says an international 
			economist. <br>
			<br>
			As Oman’s economy is expected to be based predominantly on oil and 
			gas production in the near future (69 per cent government revenue to 
			come from oil in the Seventh Plan), the government has envisaged a 
			staggering RO 4,474 million (34 per cent of the total investments) 
			for investment in this sector. To boost the non-oil sector, the 
			government expects natural gas-based industries to grow at an annual 
			growth rate of 14.5 per cent. The main hub for natural gas-based 
			industries is going to be Sohar Industrial Area where a number of 
			such projects, including Sohar Aluminum Smelter project, 
			Polyethylene project and Oman Aromatics project (at a total cost of 
			RO 2.8 billion) are coming up. Similarly, the annual growth rate in 
			tourism is projected to be 7 per cent. The US$200 million 
			ShangriLa’s Bar-Al-Jissah project is nearing completion and it will 
			be a big boost to tourism. Then, there are the other mega projects, 
			including The Wave and the Blue City. The non-oil merchandise 
			exports of Omani origin are expected to grow at 11.60 per cent.<br>
			<br>
			<b>Privatisation drive</b><br>
			The private sector is going to contribute mainly with investments in 
			gas-based industrial projects (RO 1,467 million), tourism projects 
			(RO 958 million), oil sector projects (RO 896 million investment) 
			and residential &amp; other projects (RO 2,704 million). <br>
			<br>
			In accordance with the privatisation policy, the government is set 
			to divest its stake in state-owned companies. The cabinet has 
			cleared a proposal to disinvest government’s stake in MSM-listed 
			Oman Flour Mills. A tender to sell Rusayl Power Company has already 
			been floated. The government is also considering disinvestments in 
			companies like Oman Fisheries, Oman Chromite, Golden Tulip Hotel and 
			Oman National Transport. The post and telegraph sector is also under 
			review for privatization. So in the next five years, the private 
			sector is all set to play an important role in many of the 
			activities currently under the sole purview of the government. <br>
			<br>
			On the impact of the plan investments, Andrew Duff, CEO of National 
			Bank of Oman said: “The banking sector is poised to tremendously 
			benefit from the spending close to US$ 13 billion committed during 
			the investment period. For corporate banking, the first opportunity 
			will be to fund the large industrial and infrastructure projects 
			lined up. This would also incentivise the Omani banks to quickly 
			upgrade their funding capacity and skill base to finance these 
			projects locally, than relying on overseas banks. And secondly, a 
			range of opportunities will arise to fund the small and medium 
			business sector who are expected to play a lead role in the 
			Sultanate’s future economic activities.” <br>
			<br>
			<b>Education &amp; employment</b><br>
			On the human resources development front, the main emphasis is on 
			education sector and employment generation. There is 22 per cent 
			illiteracy among those in the age group of 10 years and above (as 
			per 2003 census). The focus of the education policy is to improve 
			the general education output, expand the higher education 
			opportunities and activation of literacy programmes. On the plan to 
			achieve the education-related goals, Macki informed, “The plan 
			strives to achieve these goals through availing of additional 
			capacity and upgrading the internal efficiency of the educational 
			system, improving its quality, and rationalizing expenditure at the 
			educational institution, particularly the higher education.”<br>
			<br>
			With more than 400,000 expatriates working in the Sultanate, 
			majority in the private sector, Oman is highly dependent on the 
			talent from foreign countries. The International Monetary Fund (IMF) 
			is also advocating greater emphasis on training and education 
			programmes. In this backdrop, the investments planned under Seventh 
			Plan in the education sector doesn’t come a surprise. <br>
			<br>
			With 68 per cent of the population up to or below the age of 24 
			years, Oman’s Seventh Plan has accorded special attention to 
			employment generation for the locals. With investments above RO 13 
			billion expected in the Plan, thousands of new jobs will be created, 
			especially for the Omanis. The government has placed greater 
			emphasis on making private sector improve on its Omanisation level. 
			In 2005, Omansiation in the private sector was merely 25 per cent 
			whereas in the government civil service, it was as 81.80 per cent.
			<br>
			<br>
			With economy growing at faster pace, in certain segments, the 
			dependence on expatriates is growing. The expatriate work force is 
			growing in construction (0.4 per cent), hotels &amp; restaurants (7.2 
			per cent), health &amp; social sector (6.6 per cent), financial 
			brokerage (5.1 per cent), petrochemicals (8.5 per cent) and 
			technical &amp; scientific field (4.8 per cent) as per October 2005 
			figures. The government needs to focus on these areas where the 
			local talent needs to be trained and developed to reduce dependence 
			on expatriates. <br>
			<br>
			On how the Plan is going to improve the health standards, Macki 
			said, “The Plan aims at providing primary healthcare, minimizing the 
			incidents of infectious diseases, control of non-infectious 
			diseases, limiting the spread of hereditary diseases and reducing 
			the rates of morbidities, maternal and infant mortalities.”<br>
			<br>
			<b>Digital initiative</b><br>
			Another notable feature of the Seventh Plan is the initiatives 
			related to Oman’s Digital society and e-governance. This will 
			include the designing and management of the e-government service 
			portal, which will serve as the main portal for all services of the 
			e-governance. A unified government network will be created to link 
			all the government institutions in Oman. “This will lead to greater 
			efficiency in the government functioning besides enhancing 
			government institutions’ interaction with the citizens,” says a 
			senior consultant with an international auditing and consultancy 
			firm. The exercise also takes into consideration preparing, 
			upgrading and implementing of the security frame for the government 
			network and the Internet as well as establishing an emergency and 
			back-up center. The international computer driving license (ICDL) 
			certificate programme for the civil service employees will also be 
			taken up. <br>
			<br>
			The civil ministries, with an allocated budget of RO 2,842.5 
			million, are going to undertake a series of infrastructure 
			development programmes. In the Muscat Governorate, a number of road 
			network projects, including construction of flyover at the airport 
			roundabout, will be carried out with a view to reducing traffic 
			congestion. The completion of the network of main roads will be done 
			to ease the flow of passenger and goods traffic among the different 
			Wilayats.<br>
			<br>
			Three dams will be constructed to protect Salalah against floods 
			besides constructing Wadi Dhaiqa dam in the Muscat Governorate. The 
			sanitary network will be completed in different Wilayats. A new 
			sanitary network will be constructed in the Wilayat of Barka. Though 
			not included in the Seventh Plan, the expansion work of Seeb and 
			Salalah airports is under study. The establishment of airports at 
			Ras al Hadd, Sohar and Duqm is also under consideration.<br>
			<br>
			In addition, the Plan entails implementing a set of other service 
			projects that include schools, paving internal roads and 
			illumination in the Wilayats, establishing water desalination 
			plants, extension of pipelines and distribution networks in the 
			Wilayats, soft loans and housing assistances, water resources 
			development, and maintenance of existing assets of the government 
			besides many other projects. <br>
			<br>
			<b>Inflation worries</b><br>
			On the flip side, there may be some cause of worry for the common 
			man. The annual inflation may go higher than the last Plan, thanks 
			to price rise in the international market during the period. In the 
			last Plan, it didn’t exceed the 0.3 per cent-mark. The government is 
			prepared to keep the inflation rate under check. Said Macki: “The 
			plan aims at maintaining the currently low levels of inflation, so 
			that the annual inflation rate during the Plan shall not exceed two 
			per cent on average, so as to meet the expected increase in prices 
			in global markets. The Plan shall strive to achieve this objective 
			through adopting sustainable fiscal policies and appropriate 
			monetary policies.” <br>
			<br>
			Another concern, which the government needs to address, is the 
			flexibility aspect. As five-year is a long period, the government 
			should review the Five-Year Plan progress on an annual basis and be 
			flexible enough to make changes as per the requirements of the 
			prevailing market dynamics, instead of rigidly following the 
			original Plan. <br>
			<br>
			<b>Greater expectations</b><br>
			A section of economists believe that the government should have been 
			more liberal in committing more funds from the reserves for the 
			development projects. And the government needs to finish the 
			projects at a faster pace. But the government has been very clear in 
			that it is looking at a sustainable growth fuelled by the available 
			resources instead of committing huge investments based on 
			speculations alone. In the Seventh Plan, Oman has taken a quantum 
			leap compared to the previous Plans in terms of committing more 
			investments in infrastructure and development projects. <br>
			<br>
			Overall, the Plan has been welcomed by a majority of the business 
			community. It has generally been termed as “designed to encourage 
			economic growth, create employment, enlarge investment scope and 
			accelerate privatisation.” Sarah Hewin, Senior Economist, American 
			Express Bank, UK, sums up as she says, With expected high oil prices 
			and focus on non oil diversification activities in the new plan, 
			Oman is set for a fast paced economic growth which is seen among 
			other GCC countries also.-Akshay Bhatnagar</font></p>
