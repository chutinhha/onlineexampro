<p align="justify" dir="ltr"><font face="Verdana" size="1">Trade ties between 
Turkey and Oman have received a major boost with the signing of a Memorandum of 
Understanding (MoU) between the Oman Chamber of Commerce and Industry (OCCI) and 
the Istanbul Chamber of Commerce. <br>
<br>
The MoU, signed recently by Eng. Salim bin Said al Ghattami, Chairman of OCCI 
and his Turkish counterpart Murat Yalcin, draws the broad contours to establish 
a practical framework for the development of stronger business relations between 
the two parties, and to set forth the procedures of cooperation, which can 
enhance the implementation of reciprocal economic objectives. The two sides 
agreed to encourage exchange of trade delegations and flow of reciprocal 
economic information, statistics and trade data. The OCCI will hold an 
exhibition of Omani products in Turkey later this year. <br>
<br>
Said Mohamad Nasser Al-Shoraiqi, Director General of OCCI: “Turkey and Oman have 
shared cordial relations for a long time. We want the private sectors of our 
respective countries to reap the economic benefits of the level of excellence 
that our countries enjoy.”<br>
<br>
Talking about the benefits of the MoU, Al-Shoraiqi further said: “Oman has 
already had a feel of the Turkish experience in the construction sector because 
a Turkish company had executed the Blue City project. In fact, Turkey offers 
good, competitive international prices, over and above their experience and 
know-how. We are confident that Oman’s association with Turkish construction 
companies, both in terms of materials and project ownership, will be beneficial 
for Oman.” <br>
<br>
Turkish textiles are famous for their quality, feel and durability. “And since 
there is not much of agricultural activity in the Sultanate due to the paucity 
of water, Turkish companies would be welcome to set up food processing units in 
the Sultanate,” added Al-Shoraiqui.<br>
<br>
The major attraction for the Turkish companies looking to set up shop in Oman is 
the liberal tax structure in the Sultanate besides the low power and manpower 
costs, said Al-Shoraiqui. As of now, the sectors that have been identified as 
good prospects for Oman-Turkey trade are construction (both projects and 
materials), textiles and food processing. <br>
<br>
Moreover, the development of industrial hubs, slightly out of Muscat, in Salalah 
or Sohar, could be a major attraction for Turkish companies, said Al-Shoraiqui. 
“The Salalah Free Zone Area that is already in place and the one that has been 
planned at Sohar, near the Port of Sohar, are of great importance. Turkish 
textile and foodstuff companies would be encouraged to set up plants there. They 
would be free to produce goods for the domestic market in the Sultanate as well 
as for exports, both within the GCC countries and the other markets,” added Al-Shotaiqui.
