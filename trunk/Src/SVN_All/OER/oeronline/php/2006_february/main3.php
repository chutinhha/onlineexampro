<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>N</b></font><font face="Verdana" size="1">otwithstanding 
the Democrats’ reservations over the existing labour laws in Oman, the United 
States is confident that the Free Trade Agreement (FTA) that it had signed on 
January 19 this year in Washington will sail through smoothly in the Congress in 
the next few months. With the signing of FTA, the Sultanate of Oman became the 
fifth Middle Eastern country to negotiate a Free Trade Agreement (FTA) with the 
United States. The agreement builds on the FTAs that the world’s biggest economy 
already has with Jordan, Morocco, Bahrain and Israel. <br>
<br>
The signing of the US-Oman FTA marks the conclusion of a process that began with 
establishment of a Trade and Investment framework Agreement (TIFA) in July last 
year. In November, after further negotiations, the US Congress was given 90 days 
notice of the US administration’s intention to go ahead with an FTA.<br>
<br>
The US-Oman FTA is also a step forward in the direction of a free-trade zone in 
the Middle East region that the US envisages to create by the year 2013. Said US 
Trade Representative Rob Portman at the signing ceremony of the US-Oman FTA: 
&quot;The agreement along with (existing) pacts with Jordan, Bahrain and Morocco, is 
an important step in the effort to establish a Middle East Free Trade Area by 
2013.&quot;<br>
<br>
The signing of the FTA is also an acknowledgement of the rapid strides made by 
Oman since its accession to the World Trade Organisation (WTO). The Sultanate 
has moved rapidly towards free market values and introduced robust regulations 
aimed at increasing its profile and continued to push foreign trade and 
investment.<br>
<br>
The FTA now needs to be ratified by the US and Omani legislatures. It will be 
interesting to see how much time the ratification process takes. In case of 
Bahrain, the ratification by the US legislature took almost a year after the 
signing of the FTA. In early January, US President George Bush had signed into 
effect the US-Bahrain FTA. However, it is being estimated that in the case of 
Oman, it should not take that long a time. In fact, the negotiations on the 
US-Oman FTA were concluded in record time. The FTA was agreed upon in October 
2005, after only seven months of negotiations.<br>
<br>
Portman urged the US Congress to pass the agreement with Oman in the next few 
months and said he was hopeful of ultimately gaining support from the Democrats 
who have concerns about the Arab kingdom's labour laws. The ratification, 
therefore, is the third and final stage of the FTA, the first and second being 
the negotiations and the signing of the agreement respectively.<br>
<br>
In 2004, the exports of US goods to Oman was valued at $330 million. The FTA 
will expand the scope for exports of automobiles, machinery, optic and medical 
instruments, electrical machinery, and agricultural products such as sugars, 
vegetable oils, beverage bases and sweeteners. Two-way goods trade between Oman 
and the US stood at $748 million in 2004. U.S. exports of agricultural products 
to Oman in 2004 stood at $20 million. The stock of US foreign direct investment 
in Oman in 2003 was valued at $358 million.<br>
<br>
Oman, a non-OPEC oil and natural gas producer, already boasts of an open market. 
While under the FTA both countries would perforce eliminate duties on almost all 
industrial and consumer goods, textiles and clothing could be a different ball 
game altogether, given the sensitivities involved on both sides. Oman is an 
exporter of textiles and clothes and the United States prefers some protection 
in the form of tariff walls to ensure its domestic textile lobby. <br>
<br>
However, agriculture trade will become almost duty-free right from day one and 
will move on to become completely duty-free in 10 years. One major area of 
interest for the industry-watchers will be the impact of the FTA on the service 
industry companies in the Sultanate, which will open its doors to more service 
industry companies of the US.<br>
<br>
Commerce and Industry Minister Maqbool bin Ali Bin Sultan has said that the FTA 
will help the Sultanate diversify its economy from one based on oil. He said the 
FTA would create employment opportunities for Oman, bringing in more goods and 
services from the world’s leading economy. In fact, the Minister’s remark about 
the employment avenues is a pointer of the Sultanate’s expectations from the 
FTA. <br>
<br>
With the signing of the FTA, American goods are likely to become cheaper in 
Oman. In fact, American goods like pharmaceutical products, games and sports 
equipment, vehicles, machinery, toys, agricultural commodities like cereals, 
dairy produce, fruits and vegetables and meats are all set to enjoy high demand 
in Oman as consumers and traders and consumers can afford more choice now, and 
more significantly, affordable prices. <br>
<br>
Investment from US companies already exists in Oman, via giants such as 
Halliburton and Microsoft. The US corporate interest in Oman is likely to see a 
spurt. AES Barka and Dhofar Power Company contain significant US investment. <br>
<br>
Dow Chemical has teamed with the Sultanate and Oman Oil Company to set up Oman 
Petrochemicals Industries Corporation, a petrochemicals complex to serve as an 
anchor for Sohar. Bechtel Corporation has signed a contract with Sohar Aluminum 
Company to construct a smelterand a number of leading .US credit and finance and 
construction firms are involved in tourism development projects across the 
Sultanate.
