<p align="justify"><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" style="font-size: 18pt">
Smart Man Oman Round-up</font><font face="Verdana" size="5" color="#FF0000"><br>
</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="1"><b>Oman Economic Review supported PEIE’s 
recent Smart Manufacturing Conference initiative. Post-event, Matt Harrison 
takes stock of what transpired with a host of attendees. Excerpts:</b><br>
<b><br>
</b></font><b>
		<img border="0" src="mar1.jpg" width="158" height="161" align="right"></b><font face="Verdana" size="3"><b>S</b></font><font face="Verdana" size="1"><b>mart 
		Manufacturing looked at a host of issues – marketing, design, 
		globalization, technology, innovation and entrepreneurship. As Executive 
		Vice President at PEIE what does innovation mean to your organization?</b><br>
		Innovation is the successful exploitation of new ideas. We want to get 
		ideas out of Oman’s tertiary institutes, into offices and factories and 
		onto the balance-sheet to help Omani manufacturers compete in the global 
		market. To be frank, and as highlighted in various sessions at Smart 
		Manufacturing – Oman’s manufacturing future success will be won through 
		the exploitation of new ideas.<br>
		<br>
		For the Omani economy as a whole, innovation is the key to higher 
		productivity and greater prosperity for all. The government has already 
		laid the foundations of an innovation-driven economy by creating a 
		stable macroeconomic environment, promoting fair and free trade and 
		improving education and skills. But there’s more to be done. To 
		hold our own in modern manufacturing –areas that PEIE is actively 
		involved in – we’ll need to innovate strongly and help current PEIE-based 
		firms upgrade. Indeed, this is why PEIE organized the Smart 
		Manufacturing Conference, we feel there’s a real need to bring business 
		people, policy makers and academics together to discuss innovation and 
		its importance to manufacturing.</font></p>
<p align="justify"><font face="Verdana" size="1"><b>We hear a lot about the 
Weightless Economy. What’s PEIE’s take on it?</b><br>
In our view, the weightless economy is shorthand for the changes taking place in 
markets across the globe. Just think back a little, only 20 years ago, phone 
conversations travelled by copper wires which carried less than one page of 
information per second. Today, a strand of optical fibre as thin as a human hair 
can transmit, in a single second, the equivalent of over 90,000 volumes of an 
encyclopaedia. This change is driven by two factors which reinforce each other – 
rapid development of technology and the opening up of markets across the globe. 
Manufacturers need to be aware of these changes. Again, I believe Smart 
Manufacturing highlighted those issues.<br>
<b><br>
What do you feel are the main challenges facing Oman-based manufacturers? </b>
<br>
One of the main challenges facing manufacturers centres around the death of 
distance. With improving technology and the Internet, manufacturers can operate 
on a global basis. For this to succeed, they need to be as professional, 
efficient and cost-effective as possible. The other challenge facing them is the 
immense competition from larger companies.</font></p>
<hr color="#C0C0C0" width="90%">
		<p align="justify">
		<img border="0" src="mar2.jpg" width="181" height="187" align="right"><font face="Verdana" size="1"><b>Dexter See, if you 
		were to give five reasons for an Omani company to consider establishing 
		a factory or a business office in China, what would those reasons be?</b><br>
		I’d say the five would be huge market opportunities, availability of raw 
		materials, abundant talents (and affordable), good infrastructure, and 
		low costs of business. But I must caution that it’s tough business in 
		China given the strong competition in every area.<b><br>
		<br>
		How important is an understanding of the regions and provinces of China 
		to a company’s plans in China and how best would you recommend for a 
		company to acquire and build such knowledge into its planning?</b><br>
		Just like anywhere else, local knowledge is an absolute must. This 
		knowledge can be in terms of market dynamics, technological trends, and 
		guanxi (relationships). I’d say, first, gather information from 
		resources you can get your hands on, be it publications, seminars, or 
		exhibitions. </font>
<p align="justify"><font face="Verdana" size="1">It would also help to talk to 
experienced investors either from similar industries or those who are operating 
in locations you want to establish in. Next, build relationships and a circle of 
influence while working the ground. When it’s time to establish a presence 
there, an office or ready-built facilities are available for lease so that risks 
are kept low. I believe investing in human resources is important in acquiring 
and building such knowledge – so be sure to put this into the plan as well. <br>
Working with distributors (from city to city) is a good way firms can make entry 
into the market and gather intelligence. Joining trade organizations and 
chambers of commerce is an excellent networking platform too. <br>
<b><br>
Column Menzies Lowe, behind the &quot;look and feel&quot; of any good design there are a 
host of carefully conceived principles: fundamental propositions that define the 
essence of the design. How challenging is it to get this type of concept/mindset 
across to manufacturers?</b><br>
As part of the Design Council’s manufacturing campaign and my role at the UK’s 
National Health Service I find myself having to define design on nearly a daily 
basis and it really isn’t that difficult, the difficulty lies in persuading 
manufacturers to part with a percentage of their profits today for the chance of 
greater returns in the future, it’s all about managing their risk.<br>
<b><br>
Do you believe that a good design should reflect a sense of human history - some 
aspect of where we've come from?</b><br>
Not really. Good design fulfils a brief, it’s really that simple. If the brief 
calls for ‘human history’ to be part of the aesthetic (although this is a very 
superficial way to look at design) then it should be considered in the design 
process but I would imagine in the main it is very rarely relevant.<br>
<b><br>
The success of a design has as much to do with its physical structure as with 
the emotion that it evokes. What are your thoughts on this?</b><br>
It depends entirely on what the actual success criteria are. In my current role 
success equates to a reduction in accidental death and harm to patients, in most 
of my previous roles it is with the increase of profits, words such as physical 
structure and emotion are only relevant if they lead you to the ultimate success 
criteria, to talk about them in isolation is rather naive, almost as if design 
and art were somehow closely related, which they certainly aren’t.</font></p>
<hr color="#C0C0C0" width="90%">
<p align="justify">
		<img border="0" src="mar3.jpg" width="181" height="187" align="right" vspace="5" hspace="5"><font face="Verdana" size="1"><b>Tim Minshall what 
		are your thoughts on the issue of knowledge transfer from academia to 
		industry?</b><br>
		There’s no doubt that universities can play a key role in increasing the 
		productivity and competitiveness of a national economy. There’s a wide 
		range of ways in which universities can transfer knowledge to industry. 
		These include: the provision of skilled graduates; making available the 
		results of publicly funded research; consultancy and ‘soft’ knowledge 
		transfer activities (such as student projects); provision of ‘packaged’ 
		knowledge (through licensing of intellectual property); and the 
		formation of spin-out businesses.<br>
		<br>
		<b>We’ve seen a lot in the press lately about how universities need to 
		become more responsive to the needs of business. What are your 
		thoughts/experiences on this?</b><br>
		In the UK, great strides have been taken in this area. What’s 
		interesting is that different universities are developing different 
		approaches to working with industry appropriate for their particular 
		regional economic needs. Companies and universities are finding that 
		there are many different ways in which they can work together. Given 
		PEIE’s decision to form a Manufacturing Discussion Group I’d fully 
		expect dialogue between Oman’s industry and academia to develop and 
		flourish.</font><p align="justify"><font face="Verdana" size="1">To be frank, there are so many 
ways in which Oman’s tertiary institutes and manufacturers can work together. 
What’s needed is a clear understanding of the ways in which each can help the 
other and this relies upon good communication and the building of long term 
value-adding partnerships. <br>
<br>
One of the areas I find particularly interesting is the way in which smaller 
manufacturers can benefit from working with universities. Hopefully, PEIE’s 
Manufacturing Discussion Group will help this process.</font></p>

