<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="3"><b>
<br>
I</b></font><font face="Verdana" size="1">nnovation 
is the name of the game at Oman Economic Review (OER), which believes in 
reinventing all the time. Monotony is something OER abhors that gets reflected 
in everything it does. So, the second edition of OER CEO Golf 2006 is no 
different. <br>
<br>
Come March 23, OER plans to bring together the big drivers of the Oman business 
world at Al Maha Golf Course once again to play golf, to make it an ideal 
corporate networking platform. The big change from the previous year’s format is 
– hold your breath – the grand finale golf will be played by candlelight. A 
novel and unique experience even for golf aficionados, indeed! Imagine a magical 
ambience of candle-lit fairways! Yes, it will be a first of its kind for golf 
enthusiasts in the Sultanate of Oman. <br>
<br>
Minoo Saher, CEO of Mustafa Sultan Enterprises, himself a serious golfer, is 
looking forward to this event hosted by OER. “I was really touched by spirit of 
the event, the way it was organized, the way the staff were serving the 
participants “the whole atmosphere was so vibrant, so full of life,” he said.
<br>
<br>
Golf legend Greg Norman equally does not mince words. On a recent visit to 
Muscat, he said, “I'm sure the OER CEO Golf event with unique features like golf 
clinic for beginners, glow golf, CEO's competing with each other... is going to 
stimulate the game of golf and help in creating the perfect environment in Oman 
to produce better local golfing talent. The events like OER CEO Golf along with 
mushrooming of golf courses are going to uplift the game of golf in Oman.” 
Norman was commenting on the forthcoming prestigious OER CEO Golf 2006 event. It 
is no exaggeration to state that the entire Oman corporate world is anxiously 
awaiting the D-day.<br>
<br>
The reason for the anxiety to play a round of golf is not too far to seek. The 
world over golf is the most sought corporate game. Want proof? In a recent 
survey published in The Wall Street Journal, 67 per cent of top Fortune 500 CEOs 
unhesitatingly put their stamp of approval on golf as a necessary tool to become 
successful. Surprisingly, the “learn to play golf” concept was almost one of the 
top three attributes. <br>
<br>
Tiger Sports Management, specialising in golf events targeted at the corporate 
world, has been brought in to manage the Sultanate’s prestigious and high 
profile golfing event. <br>
<br>
TSM has been at the forefront of developing the Indian PGA Tour and has played 
an important role in the outstanding success of several high profile events in 
India and abroad. A respectable host and a reputed sports manager are all set to 
serve and service the needs of the crème de la crème of Corporate Oman.<br>
<br>
TSM has chiseled out a unique format under which OER CEO Golf 2006 will be held. 
It will involve a day and night of intense golfing action, where the main event, 
a nine-hole affair will be played in Stableford format. This year, Glow Golf, an 
exciting variant of night golf, will be played out. To cap it all, a Golf Clinic 
will be organised to help amateurs sharpen their golfing skills while 
introducing freshers to the world’s prestigious corporate pastime. Following the 
grand finale that may pit the Who’s Who of Corporate Oman, under the star-lit 
March skies and candle-lit fairways, chieftains will take part in the high 
powered networking dinner to bring the curtain down on the mega Corporate event 
penciled out.<br>
<br>
OER CEO Golf 2006, the hosts promise, will witness glitz and glamour along with 
its share of memorable moments, exciting prizes and elements of surprise. 
Winning is not everything, participation is the key as one may say so. For the 
first time in Oman, an event of this scale has been specifically conceptualised 
to invite amateurs and non-golfers for playing golf. Of course, it is true that 
one does not need to be a professional golfer to enjoy business golf. The spirit 
of the event is to come, enjoy an interesting game of golf and have a great 
time. Amen! </font></p>
