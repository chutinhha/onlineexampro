<p align="justify" dir="ltr"><font face="Verdana" size="1">
<b>by Hamza Wasi Hashmi, Head of MDS, Octara</b><br>
<br>
</font><font face="Verdana" size="3"><b>F</b></font><font face="Verdana" size="1">rom 
the lofty peaks of the Himalayas to the fertile plains of the Indus Basin, 
Pakistan exudes a colorful history dating back to over 5000 years. Today 
Pakistan is the most dynamic destination in the region for investment in the 
communications, media, IT, textile value addition, engineering, agriculture and 
allied sectors. Despite more than its fair share of extremism, bomb blasts and 
man-made calamities, Pakistan has succeeded in drawing the world’s attention to 
its fast growing markets and tremendous reserve of dedicated human resource. The 
country has become a model in retirement of public debt and Foreign Direct 
Investment in 2005-2006 is expected to exceed US$ 3 billion. <br>
<br>
However a lot is needed to provide adequate international exposure to the 
country and the image building exercise must continue in full force with the 
support of the state as well as the corporate and civil sectors. <br>
<br>
The Management Association of Pakistan is the apex body of corporate and 
business heads and leaders, which is dedicated to the representation and 
development of the business, industrial, commercial and economic sectors of the 
country. MAP serves to protects the position of the Pakistani business and 
corporate sector at all platforms and regularly organizes activities such as 
conferences, training workshops, seminars and such to promote, groom and nurture 
the corporate warriors of Pakistan.<br>
<br>
The year 2006 promises to be one of the top years of the Pakistan economy with 
foreign investments and privatization at full swing and the economy on an 
overall boom with foreign exchange reserves in excess of US$ 10 billion and an 
export target of US$ 17 billion. This growth needs to be celebrated and 
international recognition and exposure is a must. The MAP realizing this need, 
has set upon the task of putting together one of the largest management and 
corporate conventions in the history of Pakistan. <br>
<br>
The 10th Management Convention of the Management Association of Pakistan 
scheduled to be held in Karachi from the 6th to 8th of March 2006 with the theme 
of “Leadership, Innovation, Entrepreneurship” has thus been conceived and put 
together and it promises to deliver on all the motives outlined.<br>
<br>
Expected to be inaugurated by the President of Pakistan General Pervez Musharraf, 
this convention promises to bring to stage some of the most important names in 
the fields of leadership, innovation, entrepreneurship, change, marketing, 
globalization and all such related business spheres. Speakers have been invited 
from across the globe to address a gathering of top corporate leaders from 
Pakistan, South Asia, Middle East, Far East, USA, and Europe. Statesmen, 
economic planners, business gurus, strategists and political and thought leaders 
would be a part of this mega showcase.<br>
<br>
Some of the speakers who would be featuring at the event include former Deputy 
Prime Minister of Malaysia and global strategy consultant Mr. Anwar Ibrahim; Ms. 
Nicole Piasecki Executive Vice President Boeing; Professor at the Harvard 
Business School Prof. Walter Kuemmerle; Dr. Mahboob Mahmood from INSEAD Business 
School; Director General of All India Management Association Maj. Gen. D. N. 
Khurana; Director Innovation from KPMG Singapore Dr. Terry Reilly; Director 
General of Emirates Post Dubai Mr. Abdulla Al Daboos; Founder of the Laughter 
Yoga Schools Dr. Madan Kataria from India; and others. <br>
<br>
From Pakistan Federal Minister for Privatization and Investment, Dr. Abdul 
Hafeez Shaikh; Governor State Bank of Pakistan Dr. Shamshad Akhtar; Chairman 
Pakistan International Airlines, Mr. Tariq Kirmani; Regional Manager of Oracle 
Corp. Ms. Samina Rizwan; among others have confirmed their participation CNN, 
Time Magazine and Oman Economic Review are the international media partners for 
the event. The other sponsors confirmed for this event include Boeing Commercial 
Airplanes, USA, English Biscuits Manufacturers, Pakistan International Airlines, 
Pakistan State Oil, and AKD Securities.<br>
<br>
A must attend event for business leaders from across the region, this event 
would offer innumerable networking, business development, relationship building, 
brand promotion, corporate exposure and learning from the top experts.<br>
<br>
Octara a leading event management company of Pakistan and UAE, has been 
appointed by MAP to manage the affairs related to this event. We here at Octara 
take pride in our numerous international events in Pakistan including the first 
Telecommunications Investment Conference, Occupational Health Safety and 
Environment Conference and numerous other seminars and workshops with world 
renowned speakers and experts. Octara through its Dubai office has also 
partnered with numerous leading organizations like Leading Minds, Etisalat 
Academy, Right Selection and others for events at the regional level. <br>
<br>
Interested organizations from around the world can contact Octara for 
sponsorship opportunities, registration of delegates and other information. Just 
drop a line to Hamza Wasi Hashmi, Octara – A Division of TCS Private Limited at
<a href="mailto:hamza.hashmi@octara.com">hamza.hashmi@octara.com</a> or call 
+92-300-8282758 &amp; +9221-4536317.
