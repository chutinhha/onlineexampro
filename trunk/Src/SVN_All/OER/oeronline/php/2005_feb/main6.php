                    <p align="justify">
                    <font face="Verdana" size="4" color="#FF0000">Complex 
                    industry structure</font><font face="Verdana" size="1"><br>
                    <br>
                    <b>Praveen Thampi provides a lowdown on the changes taking 
                    place in the complex automotive industry</b></font></p>

                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="im16.jpg" align="left" hspace="5" width="125" height="147">I 
                    quite liked the idea of talking about the automobile history 
                    with some good information collected from reliable sources. 
                    The scope is so large and the efforts have been beyond 
                    normal description that whatever is written just does not 
                    feel good enough.<br>
                    <br>
                    The need to survive and build a lasting impression on the 
                    aspiring people remained the biggest challenge for any 
                    automobile brand world over. Some did it well by constantly 
                    changing the way they looked at building cars while some 
                    others did it by keenly observing what people wanted and how 
                    that “want” changed all the time. Many just followed the 
                    bandwagon and remained afloat. Constantly shifting “needs”, 
                    interesting innovations and environment had their effect on 
                    the industry and we saw some good things happening to 
                    mankind. <br>
                    <br>
                    Automotive promises came in differently from the different 
                    parts of the world. And it is important to look into the 
                    automobile history in America and Japan where change came 
                    through at an exciting pace. <br>
                    <br>
                    Look back and you will see that the automobile was born in 
                    Europe. However, it quickly took on an American character 
                    after the United States industry began in 1896. And not just 
                    an American character, a Midwestern rural American 
                    character. The men building the first cars in America were 
                    for the most part from rural backgrounds in the Great Lakes 
                    area - Henry Ford, Ransom Olds, Charles B. King and David 
                    Dunbar Buick, of Michigan, and Alexander Winton and James W. 
                    Packard, of Ohio, Elwood Haynes and Edgar Apperson, of 
                    Indiana, Thomas B. Jeffery, of Wisconsin. <br>
                    <br>
                    The first production car in the United States was the 
                    Duryea, built by Frank and Charles Duryea in Massachusetts, 
                    but they were from Illinois. When Chrysler Corp. acquired 
                    American Motors in 1987; the Chrysler branch of the 
                    automotive family tree took on the numerous ancestors of AMC 
                    in combination with its own complex heritage. GM’s branch 
                    already contains hundreds of companies through acquisition. 
                    And the three surviving American automakers, out of 
                    literally thousands, can all trace common ancestry back to 
                    the turn-of-the-century machine shops of Leland and 
                    Faulconer and John and Horace Dodge.<br>
                    <br>
                    Talking of America, by far the largest automobile market in 
                    the whole world for many years, evolution was dramatic. 
                    Depending on whose figures you would want to accept, one in 
                    six or seven working Americans would be engaged in the 
                    building, selling or maintenance of motor vehicles. <br>
                    <br>
                    The American Census Bureau says auto dealerships account for 
                    28.5 per cent of all retail business in the United States. 
                    However the competition levels continuously got fierce and 
                    among thousands of auto companies that have produced cars in 
                    the United States in the hundred years of the auto industry 
                    in the country, only three majors survive. These three are 
                    observed to be the products of a weeding out process of 
                    intense rivalry. Despite this rivalry, or as a matter of 
                    fact because of it, Ford Motor Co., General Motors and 
                    Chrysler Corp. are closely related — by mergers, by 
                    intermarrying with top executives of rival firms and through 
                    other business relationships. <br>
                    <br>
                    Even as early as the mid 80s, the Americans and the 
                    Europeans depended pretty much on the mass production 
                    techniques very similar to the ones introduced by the 
                    grandfather of automobiles, Henry Ford. However the Japanese 
                    moved forward and pioneered their own standards of 
                    production, which the Americans and the Europeans found very 
                    difficult to compete with. The Japanese gained substantial 
                    market share globally and while they continuously 
                    encountered political resistance, the Western counterparts 
                    rather than learning from the Japanese spent more time in 
                    establishing trade barriers. <br>
                    <br>
                    Japanese companies were sweeping the world, and the Japanese 
                    auto industry soared above competition from the so-called 
                    pioneers. It was compulsion stemming from a dire need to see 
                    the pioneers of automobiles survive that the core areas of 
                    automobile manufacturing such as market assessment, product 
                    design, detailed engineering, co-ordination of supply chain, 
                    operation of factories, marketing, sales and service of the 
                    finished product, was studied in depth by the Massachusetts 
                    Institute of Technology (MIT) team. A groundbreaking 
                    analysis of the move from mass production to lean production 
                    was carried out. This compelling in-depth study of the 
                    practices of 90 auto assembly plants in 17 countries and 
                    their interviews with individual employees, scholars, union 
                    and government officials, study highlighted the specific 
                    manufacturing techniques behind Japan’s success and also 
                    elaborated on how the Western industry can implement these 
                    innovative methods. <br>
                    <br>
                    Lean production is said to be a manufacturing system that 
                    results in a better, more cost-efficient product, higher 
                    productivity, and greater customer loyalty. The hallmarks of 
                    lean production are teamwork, communication, and efficient 
                    use of resources. And most interestingly, the results are 
                    remarkable: cars with one-third the defects, built in half 
                    the factory space, using half the man-hours. This study did 
                    much more than just let the Western world know what it takes 
                    to stride ahead when times are tough. The fundamentals of 
                    this system are applicable to every industry across the 
                    globe and will have a profound impact on human society. It 
                    would truly change the world.<br>
                    <br>
                    In the mid-80s the dilemma that faced the automobile 
                    industry was driven by the desperate need to solve the 
                    environmental and energy issues caused by the usage of cars 
                    and trucks in this world – the long term Greenhouse effect 
                    caused partly by the carbon dioxide given away by the 
                    automobiles.<br>
                    <br>
                    It is interesting to know why the structure and strategy of 
                    the dominant companies in the automobile industry changed in 
                    the way they have done. An educated view would suggest three 
                    interesting, simple and practical areas that made a 
                    difference. The real knowledge of car production and the 
                    different kinds of customers, the capability of the 
                    technological system, and the business environment would 
                    fairly define the factors at work. The knowledge system 
                    represents the level of know-how and the availability of 
                    information. <br>
                    <br>
                    In a sense, the knowledge system in the automobile industry 
                    refers to the level and the degree of diffusion of knowledge 
                    of how to make a car on the one hand and the know-how of 
                    customer demands on the other. In the history of the 
                    automobile industry the knowledge system has improved 
                    continuously and gradually. The technological system of car 
                    production consists of clusters of technologies that 
                    together form the technological base necessary to support or 
                    shape the car production. The business environment refers to 
                    the social system (economy, prosperity), the market system 
                    (the quantity and direction of the market demand), the 
                    intensity level of competition, the culture, the 
                    legislation, etc. <br>
                    Manufacturers aim to provide innovative and unique 
                    automotive products and services that deliver value. One of 
                    the many challenges faced by the automotive industry today 
                    is to deliver deals that outperform the competition. But 
                    elements such as product quality are essential in the 
                    manufacture of motor vehicles and can’t be compromised. 
                    Instead, major manufacturers need to find new and innovative 
                    ways of minimising costs. Utilising the strengths of the 
                    renowned and the best in the industry was a preferred means 
                    of developing great cars at a reasonable cost and also 
                    deliver phenomenal value to the customers. <br>
                    <br>
                    This attempt made much difference within the industry and 
                    what it led to was as exciting. Consolidation of the global 
                    automobile industry moved forward at a breathtaking pace. In 
                    2003, the six leading automobile groups accounted for almost 
                    66 per cent of the worldwide production of 60.6 million 
                    vehicles while they produced 58 million in 2000, 56.3 
                    million in 2001 and 58.8 million 2002. <br>
                    <br>
                    A good example of the industry’s continuous effort to meet 
                    expectation would be a small group of designers at a 
                    Colorado company working to revolutionise the way cars are 
                    designed and built. One key to this is replacing steel 
                    bodies with plastics. Its development goal is to design a 
                    vehicle that can achieve 100 mpg and almost zero emissions, 
                    yet has the same passenger and cargo capacity as a mid-size 
                    SUV, and can be profitably produced at the 50,000 units per 
                    year level. <br>
                    <br>
                    The prototype of this vehicle is named, aptly enough, the 
                    Revolution. Using composites as a body material can greatly 
                    reduce the complexity of a vehicle by markedly decreasing 
                    part count. Hypercar estimates that a conventional car body 
                    is constructed of between 250 and 300 separate stamped steel 
                    parts. By contrast, since plastic can be formed into more 
                    complex shapes than steel, the Revolution has only 14 major 
                    components and a total of only 62 components in its entire 
                    structure. Besides, tooling costs can be minimised since you 
                    don’t need the expensive hard tools necessary to stamp all 
                    of that steel, nor do you need the complex jigs that hold 
                    metal parts in place while they are welded. <br>
                    <br>
                    To deal with the labour-intensiveness disadvantage, Hypercar 
                    is trying to take experience gathered in the aerospace 
                    industry and modifying it to fit automotive. Many such 
                    efforts and many days and nights spent by the geniuses who 
                    give us what we want have given us the liberty to ask for 
                    more all the time. And human as we are, we continue to do 
                    that well. When cars were long, low and wide, the nags 
                    complained. Now they are whining about cars that are 
                    shorter, higher and practical.<br>
                    <br>
                    What is motivating auto buyers in this strange economy? 
                    Unlike popular beliefs, it is not the prices or the 
                    incentives. It is the growing number of new types of 
                    vehicles that is of interest. New stuff always creates 
                    markets and isn’t that good enough reason for our 
                    manufacturers to continuously create it? <br>
                    <br>
                    Strange as it may seem, the lifestyle influence, dream-led 
                    demands on what should be in a car, a never before need to 
                    distinguish oneself from the crowd and the sheer passion to 
                    feel absolute driving pleasure by many enthusiasts kept the 
                    automobile manufacturers on their toes. Was it unfair one 
                    might want to ask; but the world is changing into a pool of 
                    unfair demands as it may seem and the challenges of such 
                    demands are happily accepted and worked on by the brilliant 
                    manufacturers who in their need to stay distinguished 
                    themselves, strive hard to remain ahead of the pack, 
                    obviously giving us the benefit of their undying passion.</font></p>

