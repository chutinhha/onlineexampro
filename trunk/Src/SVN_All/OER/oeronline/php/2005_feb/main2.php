<p align="justify"><font face="Verdana" size="4" color="#FF0000">Fast encounters</font><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="1"><br>
<b>He’s fast. He’s fiery. And he’s a foodie. Pete Bassi, Chairman, Yum! 
Restaurants International, was on a whistle-stop tour to Oman as part of his 
annual world trip to touch the most important countries in Yum’s portfolio. Yum! 
is the umbrella organisation for Pizza Hut, KFC and Taco Bell. Vicky Kapur gets 
face-to-face with the fast food veteran for a quick chat. Excerpts:</b></font></p>

<p align="justify"><font face="Verdana" size="1"><b>
<img border="0" src="im14.jpg" align="left" hspace="5" width="125" height="166">How 
popular are Pizza Hut and KFC in the Middle East as opposed to McDonald’s or 
Burger King?</b><br>
We feel great about both KFC and Pizza Hut. Clearly, KFC and Pizza Hut 
individually outnumber McDonald’s or any other fast food chain in terms of 
outlets as well as sales. In their own right, both Pizza hut and KFC are the No. 
1 brands in the Middle East. There are 14 Pizza Hut and 11 KFC outlets in Oman 
whereas there are close to 700 outlets of Pizza Hut and KFC (combined) in the 
region, which includes the Middle East, North Africa, Pakistan and Turkey. <br>
<br>
<b>What’s your strongest brand in Oman?</b><br>
Pizza Hut is, without a doubt, our strongest brand in the country. We get around 
14,000 visitors a week in our outlets across Oman. <br>
<br>
<b>In this land of shwarma and filafal, what strategy does Yum! employ to 
increase consumption of pizza and chicken in the bucket?</b><br>
The way we run our business is that we standardise most parts of our brands 
internationally, and then we try and localise them for the various regions. With 
the help of our local franchises, we undertake local adaptations of our global 
recipes to suit the palate of the people. For instance, in the case of a pizza, 
some of the spices and condiments used to garnish the pizzas are adapted 
according to the local palate while the pizza bases and the toppings are the 
same everywhere in the world. <br>
<br>
So, we advise all our partners to be very careful about taking as much as they 
can from the global standard, but not ignoring the local needs. For instance, 
while KFC’s Twister is a worldwide product, introduced first in Australia, it is 
one of the largest selling items on KFC’s menu in the region. That, I believe, 
is because it has an easy acceptability and resemblance to the local shwarma. Of 
course, while you will get the same Twister at any KFC outlet in the world, the 
sauce to go with it will be adapted to the local taste. That’s one example of 
thinking globally and acting locally.<br>
<br>
<b>Beef seems to be more popular than chicken in Oman and, indeed, the rest of 
the Middle East. How do you tackle that issue with regard to KFC?</b><br>
I don’t think it’s an enormous issue. Beef is more popular than chicken even in 
America. I think the key is whether the market is big enough, period. Of course, 
it is more than big enough. I don’t think beef being more popular is a factor at 
all. <br>
<br>
Also, I think chicken is the most preferred protein in the Middle East – by far. 
I do not have the figures, but chicken has one of the highest per capita 
consumptions in the region. <br>
<br>
Moreover, there are certain kinds of food that people prefer to cook and eat in 
the house while there are others that they relish outside. Beef may be popular, 
but chicken is what people in the Middle East prefer to eat outside, and that’s 
what we care about. <br>
<br>
<b>What is your estimate of the size of the global market for American fast 
food, and how rapidly is it growing?</b><br>
As of today, we have around 12,000 restaurants globally. We’re opening roughly 
1,100 outlets a year and closing 300 to 400. I reckon that we will soon be 
adding around 1,700 restaurants a year, depending on the pace of growth of the 
market. In fact, Middle East has the broadest representation of American fast 
food restaurants. Then there’s China, which is growing at a phenomenal rate. In 
fact, we have 1,500 restaurants in China, and are opening 300 restaurants in the 
country per year – i.e., one restaurant almost every day! The global market for 
American fast food is growing enormously. <br>
<br>
<b>Why do you think this market is growing at such a rapid pace?</b><br>
There is no magic in American fast food – the only thing magical is perhaps that 
large American companies have aligned very capable local partners. We can 
provide the operating system and they can provide local know-how to emerge 
winners. It really doesn’t matter what culture you are operating in – if you 
serve people good tasting food at reasonable prices in a nice environment with 
excellent service, you can do business! <br>
<br>
<b>You are credited with making KFC and Pizza Hut ‘hotter’ than McDonald’s in 
many major international markets. How have you managed to achieve this?</b><br>
It all boils down to having better resources initially in certain pockets, ahead 
of your competitors. We do much better in Asia and the Middle East because we 
have had better resources than McDonald’s at an early point of time. The reverse 
is also true – McDonald’s is much bigger than us in Western Europe because we 
didn’t go into France and Germany earlier – we’re just going in now. It’s 
usually a matter of who has better resources. <br>
<br>
Occasionally, you have an advantage in terms of a preferred protein – for 
example, in China, KFC does have an advantage because chicken is a vastly 
preferred protein, and McDonald’s is obviously going to struggle in India 
because beef is not popular there. But usually it is a case of a company along 
with its local people having done some good work years ago that develops 
momentum and capability, coupled with global support. In markets where we do not 
own our own brands, our franchise partners are key to our success. Case for 
example is right here in Oman – we have been here for 16 years, and we are with 
the same partners (Khimji Ramdas) who are very passionate and committed to the 
whole business. <br>
<br>
<b>In such a large chain of restaurants with outlets all across the globe, how 
do you maintain focus?</b><br>
With great difficulty. It’s probably the biggest challenge that we face today. 
We try to do this in two ways – each year we shortlist three or four major 
initiatives that we undertake and that we’d like everybody to focus on. Now, 
some of them may be new for the year while others could be the same year after 
year. The other thing is that we hold certain things sacred. These are things 
that we’ll never allow deviation from – for example, the food. So, you keep your 
major initiatives for a year to a simple, clear number. <br>
<br>
<b>How do you communicate those objectives to such a large number of franchisees 
and make sure that the message goes through?</b><br>
We have our annual meetings with the 15-20 key general managers, who are part of 
the discussions on what we should do next year. We had a major meeting in Dubai 
almost five years ago, where we identified that our biggest problem in the 
region was that we were having trouble opening new restaurants at the pace we 
would have liked to open them due to various reasons. We were adding 700 
restaurants a year while we wanted to add around 1,200. So we identified 
developing the new restaurant business as the major initiative for the coming 
few years. It worked, and three years later, we were opening 1,000 restaurants a 
year. <br>
<br>
There is also a very well thought-out communication regime in place. So, while 
we meet once or twice a year, our functional colleagues—those responsible for 
operations, marketing, human resources etc.—meet once a year too. So, that’s how 
the message gets communicated throughout the organisation and to the franchise 
partners. Then we have our annual convention where we get all our franchise 
partners together and communicate our goals and strategies to them. That’s how 
we achieve ‘worldwide alignment’ as we call it. <br>
<br>
<b>What would be the one thing that you would never compromise on in your 
business?<br>
Food, without a question! </b><br>
<br>
How do you maintain employee motivation in disparate regions of the world?<br>
It’s something that we spend a great deal of time on. We have a ‘reward and 
recognition’ culture, which we are very proud of. We recognise the fact that 
people need to be appreciated. In most cultures, appreciation is more important 
than money. We involve people right from the top to the bottom of the 
hierarchical ladder and our people are very passionate about the company. <br>
<br>
<b>What impact has PETA’s KFC Cruelty campaign had on your sales? What 
improvements are being made?</b><br>
We believe that it is important that we comply with government regulations in 
each and every market that we are present in. There are always going to some 
groups that will want things to be done differently – we respect their opinion, 
but the fact is that we make sure that we abide by the laws of the land and we 
make sure that we r doing things accordingly. <br>
<br>
<b>How does Yum! Restaurants International participate in the global fight 
against obesity?</b><br>
In markets where obesity is becoming a problem, we are putting a lot of efforts 
to give people options on the menu, so that people who want to eat lighter or 
less calories or want to eat healthier, get those options in those markets where 
health concerns are rising. Consumers drive our business – what our local 
franchise partner is doing and what we are doing is listening to what the 
consumer wants. If a consumer in a market wants something and we think that we 
can make it work, then we’ll provide that food. However, just because someone 
outside the consumer base thinks it is important, it might not be that 
important.<br>
