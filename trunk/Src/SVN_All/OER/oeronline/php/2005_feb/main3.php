<p align="left"><font face="Verdana" size="4" color="#FF0000">Marketing in C</font><br>
<br>
<font face="Verdana" size="1"><b>Taking marketing strategy beyond “the 4 Ps”. By 
Ward Roofthooft, MCE Faculty Member</b></font></p>

<p align="center">
<font face="Verdana" size="1">
<img border="0" src="im10.jpg" width="389" height="290"></font></p>

<p align="center">
<font face="Verdana" size="1">”Primum vivere et deinde filosofare.” <br>
(Latin proverb: ‘‘First live and then philosophise’’ or: ‘‘Fundamentals 
first’’.) </font></p>

<p align="justify">
<font face="Verdana" size="1"><b>What is wrong with the “4 Ps Model” of 
McCarthy-Kotler? </b><br>
It has served us well during a quarter of a century. <br>
• If we position our PRODUCT correctly, <br>
• If we choose the appropriate market PLACE (distribution policy), <br>
• If we fix the right PRICE, <br>
• If we make adequate PROMOTION... <br>
We have sewed it up. Haven’t we?<br>
<br>
Apparently not, because you cannot find these days a marketing congress or 
symposium without at least one new “P” on the programme: Personnel, politics, 
policy, pennies, power, principles, practice, public relations, public interest, 
and plenty of other new “Ps” that will supposedly give a company the determining 
competitive edge. <br>
<br>
The time has come to accept and respect the “4 Ps” of McCarthy and Kotler for 
what they are: a handy operational tool for the professional marketer who has 
first established his fundamental marketing strategy. This strategy should 
reflect the essence of the company’s strategy more deeply than the “4 Ps” can 
do. As will be shown further in this article, such a fundamental marketing 
strategy is even more indispensable in International Marketing. There is clearly 
a need for a strategic structure that takes into account the turbulence that has 
been caused by the globalisation of business and by the explosion in 
communication. <br>
<br>
Cultures and subcultures emerge and disappear today with dazzling speed. When 
Kotler wrote his standard work on marketing in 1967, many cultural and social 
movements with direct impact on marketing did not even have a name, most 
probably because they did not exist or were not recognized and were therefore 
irrelevant to marketing. Yes, the teenagers existed already. But what about the 
“Baby boomers” (People born shortly after the Second World War), the “Yuppies” 
(Young Urban Professionals), the “Dinkies” (Double Income No Kids), the “Third 
age”, the “Fourth age”, the “Joggers”, the “Punks”, and the “Weight Watchers”?
<b>Yet, all of these people have money to spend and are waiting for marketing to 
pay them attention and respect. </b><br>
<br>
<img border="0" src="im12.jpg" align="right" width="250" height="169">For the international marketer there is the additional spectrum of national 
cultures. It is true that the Big Mac has achieved some worldwide acceptance but 
it will still be some time before it will replace the tepanyaki of the Japanese, 
the chile poblano of the Mexicans, or the Sauerkraut of the Germans. Frankly, we 
believe it will never happen. Real culture is sitting a lot deeper than the 
pocket from which a dollar or a euro is waiting to jump to the satisfaction of 
some consumer need. The Magna Carta has survived a millennium of turbulent 
history. We believe that the Magna Mac will not last that long. <br>
<br>
Yet, culture is conspicuously absent from the minds of many international 
marketers. How many managers have even thought about their own company’s 
culture, let alone the many cultures of their international partners and 
customers? This era is probably not more “cultural” than previous times. But it 
looks like ever greater numbers of people are aware of their (sub) culture and 
want to have it recognised. How else should one explain the proliferation of 
countless subcultures in our society, each one of them claiming a place in the 
media and the attention of business, of the authorities and even of the arts?
<br>
One and the other leads to the conviction that one cannot even start considering 
a marketing strategy without a strong awareness of one’s own company culture and 
the culture of one’s customers. That makes CULTURE, that of the COMPANY and that 
of the CUSTOMER the first “C” in “Marketing in C”. <br>
<br>
A second important notion that is missing in the classical marketing mix 
thinking is that of imperfection. Marketers seem to believe that there is a 
point of absolute nirvana at which the customer is completely satisfied with the 
product or the service offered. They even seem to overlook the trivial fact that 
others, called competitors are also vying for the customer’s favours. This 
attitude is reflected in the marketing language. There is probably no other walk 
of life that uses so much superlatives, bragging, bluff and exaggerations in its 
language than marketing. </font></p>

<p align="justify">
<font face="Verdana" size="1"><b>Yet, isn’t there a permanent state of CONFLICT 
at the customer’s level? </b><br>
Isn’t there a conflict between the customer’s needs and the imperfect solution 
which any product or service is offering? Isn’t there an additional conflict, 
namely that of the choice that a customer must make between this product or 
service and those of its competitors? The modern customer, in consumer marketing 
as well as in industrial marketing seems to have grown up. He understands that 
nothing in this world is perfect, not even his problem and consequently also not 
the solution to that problem. He seems to be willing to settle for the best 
possible solution offered under these circumstances, at the price asked. And he 
keeps his eyes wide open for alternative solutions: those of the competition. 
That makes the second “C”: CONFLICT, at the level of the CUSTOMER and 
complicated by the actions of the COMPETITION. <br>
<br>
Once all this has been understood, there is the need to communicate about it. 
Such communication will be determined by the need for harmony between the 
company’s culture and the culture of the customer. It will recognise the 
conflict of the customer and the evil deeds of the competition. It will be a 
vastly different communication from the one that would start with the “P” of 
“Product” which is of course purely a company starting point. <br>
<br>
The third “C” is therefore that of COMMUNICATION, both in its CONTENT and in the 
CHANNELS chosen to communicate. <br>
<br>
The full process of marketing: Attention - Interest - Desire - Action (“AIDA”) 
is too long, too costly and too hazardous to be followed for each sale. The road 
should be shortened and this is possible if a CONSENSUS can be reached between 
customer and company. It means that marketing must turn the customer into a 
partner who shares the life of the company. Instead of chasing individual sales, 
marketing should be concerned with developing the customer so that he continues 
to buy and is willing to give information about the market and the competition, 
and even about the strengths and weaknesses of his supplier. <br>
<br>
That leads to the fourth “C”: CONSENSUS with the CUSTOMER but also in the 
COMPANY where everybody will be a member of the marketing function since a 
company can only exist by the grace of satisfying customers needs. </font></p>

<p align="justify">
<font face="Verdana" size="1">Thus, the “Marketing in C” model has come full 
circle. <br>
• CULTURE COMPANY <br>
• CUSTOMER <br>
• CONFLICT CUSTOMER <br>
• COMPETITION <br>
• COMMUNICATION CONTENT <br>
• CHANNELS <br>
• CONSENSUS CUSTOMER <br>
• COMPANY </font></p>

<p align="justify">
<font face="Verdana" size="1">Quite a number of “Cs” isn’t it? Yet the term 
“Marketing in C” covers more than the coincidental occurrence of the letter C as 
the first letter of each element of the model. It is also an analogy with music. 
In music, the scale of C-major is the basic scale, with no sharps and no flats: 
the basics, the essential. In much the same way, “Marketing in C” tries to 
concentrate strategic thinking on the very essential role of marketing in the 
company’s (international) business. </font></p>

