<p align="left"><font face="Verdana" size="4" color="#FF0000">Budget 2005</font><br>
<b><font face="Verdana" size="1">By Lakshmi Kothaneth</font></b></p>

                    <p align="justify"><font face="Verdana" size="1"><b>Budget 
                    Objectives</b><br>
                    <img border="0" src="im13.jpg" align="left" hspace="5" vspace="3" width="175" height="142">The 
                    objective of a budget is to diversify the economy, HE Ahmed 
                    bin Abdulnabi Macki, minister of national economy and deputy 
                    chairman of the Financial Affairs and Energy Resources 
                    Council said recently when he unveiled what the media here 
                    described as a ‘‘cautious, prudent and fundamentally tough 
                    budget for 2005’’.<br>
                    <br>
                    ‘‘The thrust of the budget is to diversify government 
                    resources and reduce dependence on oil revenues as the main 
                    source of budget,’’ HE Macki said. And this was evident from 
                    the previous budgets that the main objective of the 
                    government was and is nationwide development, he stressed. 
                    The budget also reflects the government’s commitment to the 
                    process of economic and structural policy reforms, he said.<br>
                    <br>
                    ‘‘We have now managed to have projects in the north (Sohar), 
                    in the middle (Al Wusta) and then in the south (Salalah). 
                    Development is seen everywhere, and not just concentrated at 
                    one place,’’ HE Macki noted.<br>
                    <br>
                    <b>Growth-oriented budgets</b><br>
                    HE Macki noted that Oman’s budgets are growth oriented. 
                    According to him, economy grew 12.5 per cent in 2004 as 
                    against 6.9 per cent in 2003. Exports and imports (actual) 
                    in 2004 are expected to register 14.2 and 20 per cent 
                    growth, respectively. The trade balance surplus is expected 
                    to increase by 6.1 per cent.<br>
                    <br>
                    HE Macki said that the 2005 budget was expected to give 
                    fresh boost to economic and structural policy reforms and 
                    established flexible investment and tax regimes to attract 
                    foreign investments, allowing free repatriation of capital 
                    and profits.<br>
                    <br>
                    The underlying factor of the RO540 million-deficit budget is 
                    that the government plans to rationalise government 
                    expenditure and augment revenues. According to HE Macki, the 
                    deficit represents 17 per cent of the total revenues and 6 
                    per cent of GDP.<br>
                    <br>
                    The excess of planned aggregate expenditure of RO3,680 
                    million over budgeted revenue receipts of RO3,140 million 
                    leaves a deficit of RO540 million, as against a deficit of 
                    RO801 million (revised) in the previous budget.<br>
                    <br>
                    • The budget deficit for 2004 was initially estimated at 
                    lower than RO500 million.<br>
                    • However, it was revised to RO801 million as the government 
                    had allocated an additional RO301 million to the public 
                    expenditure during the year.<br>
                    <br>
                    Oil revenues recorded an increase of RO1.252 billion in 
                    2004, wiping out the entire budget deficit for 2004. The 
                    additional revenue has covered the initially estimated 
                    deficit of RO500 million, RO301 million arisen out of 
                    additional allocations and payment of foreign loans 
                    amounting to RO207 million.<br>
                    <br>
                    The current year’s budget is based on an average price of 
                    $23 a barrel for Oman crude. Oil production for 2005 has 
                    been estimated lower at 753,000 bpd as against 909,000 bpd 
                    estimated in the current five-year plan. Oil revenue 
                    budgeted for the current year accounts for 65 per cent of 
                    total revenues. Gas revenues and current and capital 
                    revenues constitute 9 and 26 per cent, respectively.<br>
                    <br>
                    <b>Oil prices<br>
                    </b>‘‘As far as the uncertainties of oil is concerned, I 
                    think in Oman, we are working on the basis that we don’t 
                    have any challenges or difficulties at the moment. As far as 
                    oil production is concerned, we have taken appropriate 
                    measures. The increase of production in gas and other 
                    revenues will balance the shortfall in the oil production 
                    and the revenue from oil,’’ HE Macki said.<br>
                    ‘‘Outside factors may affect oil price, but we do not want 
                    the prices to increase out of the grievances of others. 
                    Frankly, various external factors have had their impact on 
                    the oil prices.’’ HE Macki said that the oil price has been 
                    set at $23 per barrel for next year.<br>
                    ‘‘We do not anticipate a drastic fall in the oil price. We 
                    feel that the oil prices are passing through a structural 
                    feature and might continue in the short and medium term. It 
                    might come down, but we don’t think it will fluctuate from 
                    the current status, which is about $34 to $35, to for 
                    instance, $20 or $18. No, we don’t anticipate that.’’<br>
                    Current indications show that most of the oil companies have 
                    taken the price of $22 and $25 as their benchmark for 
                    planning purposes. ‘‘If oil prices steady to about $30, we 
                    will be able to handle the deficit. Any surplus will be used 
                    to handle the deficit, or, we will move to the international 
                    market for recourse.’’<br>
                    <br>
                    <b>Oil production</b><br>
                    The decline in Oman’s oil production has been attributed to 
                    a drastic fall in the daily output of Petroleum Development 
                    Oman (PDO). PDO’s daily output fell drastically from 840,000 
                    barrels in 2000 to 656,000 barrels in 2004. Though the 
                    budget for 2004 was based on an average price of $21 a 
                    barrel for Oman crude, the actual price realised during the 
                    year was $33.9 a barrel. The increase in Oman crude prices 
                    added additional oil returns of RO291 million in 2004.<br>
                    <br>
                    <b>Budget allocation</b><br>
                    The budget exhibits the government’s commitment to the 
                    process of economic and structural policy reforms. The 
                    government has allocated RO1.148 billion for defence and 
                    national security, accounting for 31 per cent of total 
                    expenditure and over 36 per cent of total revenues. It has 
                    allocated RO1.356 billion for civil ministries’ expenditure.
                    <br>
                    <br>
                    For oil and gas production, RO168 million has been 
                    allocated. The budget has given due importance to 
                    educational and health sectors (RO267 million), and another 
                    top priority sector – human resources development (RO546 
                    million).<br>
                    <br>
                    The budget has allocated RO66 million as soft loan for 
                    sectors such as agriculture and fisheries, industry, tourism 
                    and education. Support has also been provided to Oman 
                    Housing Bank and Oman Development Bank to finance sewage 
                    projects in Muscat and Salalah. <br>
                    <br>
                    The current year’s budget deficit will be financed through 
                    foreign borrowings of RO150 million and RO390 million from 
                    the State General Reserve Fund. Allocation for new projects 
                    during the last year of the five-year plan (2005) amounted 
                    to RO238 million. <br>
                    Sumail hospital, Nizwa/Thumrait road, sewage networks in 
                    nine towns, water network extension at Al Khoud/Al Hail, 
                    schools, Baushar/Al Amerat road and road network in Masirah 
                    are the projects that will be completed this year.</font></p>

