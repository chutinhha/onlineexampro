<font face="Verdana" size="5" color="#FF0000">How do you want your car?</font><font face="Verdana" size="1"><br>
<br>
<b>Praveen V. Thampi claims that without major fixes, the current method of 
retailing cars faces major challenges from well-capitalised, shrewd outsiders 
who could take over the business whether dealers and automakers like it or not<br>
</b>
<br>
</font><b><font face="Verdana" size="3">
<img border="1" src="auto_mate.jpg" width="163" height="197" align="left" vspace="1">B</font></b><font face="Verdana" size="1">uilding 
on further into my previous article, I see much coming into the table when we 
talk about the retail business and look at just how important it is for any 
brand to position themselves right, utilising the different means of 
communication and addressing the different basic senses of the human being. From 
my perspective, brand value and perception plays an important role in the whole 
equation.<br>
<br>
It is a fact well known that customers do not buy a car that is not known to 
them, by dumping what has always been around and given them the feel of their 
own, unlike just about most other consumer durable goods. There are people who 
would buy a particular brand of car, because they always had. Research proves 
that if any attempt is made to dilute the aura of uniqueness each auto brand 
exudes, serious trouble could erupt. It is also a fact that if at any time 
marketing efforts are made to convince the consumer that one car is really like 
another, then a lot of manufacturers are going to go under, never mind the 
dealerships that market their products. Only the most efficient handful of 
makers will survive – and not very profitably. <br>
<br>
What would happen most likely is that consumers would simply stop buying cars. 
They would start viewing them, because of their cost, like houses, buying a 
cheap one when they are getting started in life, then 10 to 15 years later, a 
little more comfortable one once they had established themselves in their 
careers. Nobody wants that – not the carmakers, not their dealers, and most 
certainly not consumers. <br>
<br>
However, consumers do want something that is compelling change in the retail 
automotive business. They are seeking ways to manage their scarcest 
resource—time—and they appreciate dealerships that help them by offering 
efficient processes. We are in an era of increased consumer expectation and 
sophistication and definitely we are not entering a time when the car becomes a 
commodity. Most car buyers remain passionate about their cars and in many ways, 
they feel defined by them. If you look back and speak with your friends, you 
will note that everyone can recall not only the first car he ever owned but 
every car he's ever owned, usually in great detail. People tell stories about 
their cars once again proving the most important fact that motor vehicles are 
not mere transportation appliances.<br>
<br>
Without major fixes, the current method of retailing cars faces major challenges 
from well-capitalised, shrewd outsiders who could take over the business whether 
dealers and automakers like it or not. An in-depth study in this area suggests a 
few key areas where changes are necessary: we need to take all the excessive and 
unnecessary overhead and investment costs out of the system, and lower vehicles’ 
prices accordingly. The entire wholesale and retail distribution, logistics and 
retailer support networks need thrifting. We have huge, unnecessary expenses in 
those areas which could be shared and consolidated without interfering with 
brand marketing. <br>
<br>
We give lip service to the consumer, but lack basic understanding of consumer 
behaviour. If you were to closely observe, the grocery industry knows almost 
everything about what influences a shopper’s decision to buy, right down to the 
details of shelf size and positioning. The fact is that no such data is easily 
available for the automobile industry.<br>
Large retail chains or automobile department stores are not the answer to the 
challenges we face. It is important to note some of the key advantages of the 
current system: the traditional dealership network already exists and doesn't 
have to be built; it has broad geographic coverage; it has an intimate knowledge 
of local markets; has a well-established and long-enduring relationship with its 
customer base; and it provides a full range of after-sales service.<br>
<br>
Strategies and policies will lead the evolution to the next era of automotive 
retailing. Knowing how and why a customer shops is vital to retailing success in 
an ever-competitive consumer market. Experts have identified the Top Ten 
emerging consumer, marketing and technology trends to help retailers position 
themselves for the new millennium. They say that Interactive Technology, New 
Lighting Techniques and Technology, Integrated Marketing, Multi-Dimensional 
Storefronts, Flexible and Cost-Effective Interior Graphics, Accommodating the 
Shopper's Need for Convenience and Entertainment, Flexibility in Fixtures and 
Complementary Designs, Growth of Home and Lifestyle oriented Products, Branding 
and Co-Branding and to top it all The Return of the Neighbourhood Retailer are 
some solutions. Some larger retailers are recognising that shoppers do not have 
time to travel far to different stores. In response, many are creating smaller 
&quot;neighbourhood format&quot; stores.<br>
<br>
Someone mentioned the need to be more practical and deal with what is called the 
Post Present. The Post Present contains vehicles of change, some of which are 
already in motion. I believe the successor to the Brand Image in the Post 
Present will be &quot;The Brand Experience,&quot; which goes beyond the attributes of a 
product. It is a gestalt that includes: the advertising, the package, where and 
how the product is sold, the price, the consumer's satisfaction from its use, 
the service provided, the continuous renewal of the product's uniqueness and the 
development of an interactive relationship that bonds the buyer and seller after 
the sale is made. Many successful companies already understand this. <br>
<br>
Corporations will manage customers and how they behave rather than products and 
what they represent. The brand manager will be replaced by the Customer Manager, 
who will be responsible for satisfying consumers' needs. A &quot;share of loyal 
customers&quot; and a consumer-centred strategy will build profits better than 
old-fashioned concepts such as &quot;share of voice,&quot; &quot;share of mind&quot; and &quot;share of 
market.&quot; A &quot;brand&quot; must become a cluster not just of a product's values - but 
also of the consumer's needs. In the Post Present, a new kind of value bundle 
will be introduced, called &quot;prodices.&quot; <br>
<br>
They look like products but act like services. I believe that people will 
increasingly want to buy what things do; rather than what they are, just as many 
drivers would rather not own a car that can break down, rust, age, lose value or 
become technologically obsolete. In that context leasing has become the 
fastest-growing segment of automobile sales as drivers increasingly want to own 
the service of transportation and not the physical car. There are many 
manufacturers at the moment exploring the sale of miles rather than cars. <br>
<br>
Consumers, as always, will choose the form of shopping that provides the most 
value and convenience. As intermediaries between the manufacturer and the 
consumer, the mega retailers provide the value of distribution, but they also 
add a cost to the products they sell. Most retailers no longer care what or why 
consumers buy as long as they buy from them. It's up to the marketers and 
advertisers to influence consumer behaviour wherever their products are sold. 
Because 70 per cent of consumer purchasing decisions are made on-site, the 
shelves of mega-retailers are now an advertising medium, and shelf space is now 
sold to manufacturers just like newspaper space and television time. These 
retailers are now the gatekeepers of distribution, and they charge accordingly.
<br>
<br>
Large general advertisers in the industry have recently demanded that their 
advertising become accountable for sales, which means advertising that affects 
only consumer attitudes, rather than a decision to buy, is likely to be too 
costly compared with infomercials, entertainment commercials, games, contests 
and other forms of interactive advertising and publishing. &quot;Contact strategies&quot; 
will replace media strategies. Relationships will count far more than 
&quot;exposure.&quot; Video-on-demand, advertising-on-demand and shopping-on-demand will 
change the way consumers watch television. TV will no longer provide mass 
audiences for advertising because masses of viewers will no longer choose to see 
the same program at the same time, except for important live events.<br>
<br>
As audiences continue to fragment, so will advertising that seeks to deliver 
relevant sales messages amid consumers' shopping for entertainment, products and 
services of their choice. Manufacturers will use these new communications media 
to know and supply the products individual customers want, through the new 
techniques of mass customisation. <br>
So, what does the Post Present imply for information-based advertising that 
affects both behavior and attitudes and creates measurable profits. Research has 
proven that loyal customers produce 90% of a brand's profit - that one-time 
sales create a loss. The marketing mathematics are persuasive. It costs six to 
10 times as much to get a new customer as it does to retain an old one. But the 
consumer's loyalty is not for sale. Loyalty requires that the consumer continue 
to be totally satisfied with a product or service. The purpose of marketing and 
advertising has always been to sell products. Hereafter it will be to sell 
products through long-term relationships. Marketing investments will be made 
against a consumer's lifetime value. <br>
<br>
The way brands build their presence in the minds of the shoppers and be present 
in their shopping list becomes a key success criteria to build any retail 
business. As much as we see much being done by the different brands to capture 
the minds of the customer, It is a known fact that the effects of branding 
campaign are on a steady decline as the cost of reaching consumers in an ever 
busier world is on the rise. With so much around, it is not easy to get that 
mind of the customer to focus on our brand if we do not get ourselves positioned 
in their minds the way they would like to keep it. Repetition has been one of 
the most prominent techniques used by advertisers to ensure a message is 
understood and remembered by the customer. A classic television campaign will be 
seen or heard by the consumer on an average of three times. There certainly is a 
limit to just how many times we can repeat what we do. <br>
<br>
To what extent we can saturate the airwaves with messages and still expect 
people to pay attention? The reality is that the people are spending less time 
in front of television, less time reading magazines, and less time listening to 
the radio. Advertising spends have increased on an average by 8 per cent 
annually. An Average consumer is exposed to 9 per cent more commercial messages 
each year. In 1965 the average consumer remembered 34 per cent of advertisements 
shown on television while in 1990 he remembered only 8 per cent. If we were to 
closely observe, household television viewing has increasingly become the domain 
of children. A study suggests that in the US, the average child is exposed to 
more than 30,000 television commercials a year while the adults see about 86,500 
commercials. <br>
<br>
It also suggest that today’s sixty five year old American has on an average 
watched more than 2 million commercials through his life, which is equal to 
watching television commercials every day, eight hours a day, seven days a week 
without any break for close to six years. Interesting comparisons for us to get 
a hang of what is in store for the brand that communicate with so much at stake. 
Given this level of television viewing facts, it is not surprising that about 
$244 billion was spent on all advertising in the year 2003. Frenzied world out 
there, you might think. However the fact remains that there is a steady decline 
in Television watching among the adults and time pressures have contributed a 
great deal into it. It is becoming increasingly difficult for any brand to 
communicate their key message by doing the same sight and sound exercise. More 
and more money is being spent executing increasingly less effective brand 
campaigns. In short advertising has hit a brick wall.<br>
<br>
With so many challenges around, normal marketing is not working the way it 
should. Customer expectations keep changing and rising as they get more value 
for what they pay for and the phenomenon just keeps building. Whether it is a 
clear value to which they can link the monetary element of buying to or even the 
emotional value to which they can identify their own person; today’s consumer is 
continuously exposed to better things in life which is made easy on them. How do 
organizations and brands continuously build on this expectation and deliver 
better products and services by consistently building on what they have to 
offer. <br>
<br>
Is it enough to make good quality products and continue to work on your 
distribution channel and then focus on the right way of retailing these 
products? How do these good cars reach out to “That” customer who the 
manufacturer made this car for? New products are failing and most advertising 
campaigns do not register anything that is distinct in the customers mind. It is 
not enough for any brand to just be seen since most of them come across as 
interchangeable commodities in time, rather than powerful brands. Distinctive 
brands require more than that and they have to be powered up to deliver a full 
sensory and emotional experience. The brand building of the future will move 
from a two sensory approach to a multi sensory approach. If we carefully look at 
the various industries; currently the brand manufacturers own their brand. As we 
see it, this has to change and in the future these brands will need to be owned 
by the customers. <br>
<br>
An interesting research conducted by Martin Lindstorm to understand the deep 
sense of brand marketing clarifies some interesting facts. This shift from the 
companies owning their brands to customer owning the brand turned out to be 
known as the “Me selling proposition” (MSP). In the 1950’s branding belonged to 
the Unique Selling proposition (USP). By the 60’s the first signs of true 
“Emotional selling proposition” (ESP) was evident and similar products were 
perceived to be different primarily because of an emotional attachment. This 
build on and soon the “Organizational selling proposition” (OSP) became the key 
word since the organization or the corporation selling the brand became the 
brand and an organization’s philosophy distinguished it from others. <br>
<br>
As expected, a change came in the 90’s and brands gained phenomenal strength in 
their own right and the “Brand selling proposition” (BSP) took over soon. At 
this time it was very evident that the brand was much stronger than the physical 
dimensions of the product. Slowly consumers started taking ownership of their 
brands and the “Me selling proposition” became a key element. There are however 
indications that branding will invariably move from “Me selling proposition” to 
“Holistic selling proposition”, which is a more sophisticated realm, reflecting 
a brave new world where a consumer desperately needs something to believe in and 
brands very well might provide it. <br>
<br>
So much more being done on this area just to get the attention of that target 
and create some space for our brands in their important mind. All this costs 
phenomenal amounts of monies and the brands are ever so willing to do it. They 
do it continuously and differently all the time or they cease to exist in this 
competitive environment. Exciting and interesting when you see success on the 
plates of these brands and you know that these basic senses that make us human, 
has to work. Space has once again become a limitation here and some very good 
examples of the efforts being made to address the five senses of the human has 
to wait until the next article. <br>
