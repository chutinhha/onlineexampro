<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">
A noble visit</font><font face="Verdana" size="1"><br>
<br>
<b>He’s a mayor, a minister of State, an ambassador, a chancellor, an admiral, 
and a chief magistrate all rolled into one. Vicky Kapur met with Alderman 
Michael Savory, the Rt. Hon. The Lord Mayor of the City of London, during the 
Oman leg of his Gulf visit. Excerpts from an exclusive interview:</b><br>
<br>
<b>
<img border="1" src="noble.jpg" width="100" height="116" align="left" hspace="5"></b></font><font face="Verdana" size="3"><b>W</b></font><font face="Verdana" size="1"><b>elcome 
to Oman, Lord Mayor. Could you please make us aware of your role?</b><br>
As the Lord Mayor of the City of London, my role is actually to act as an 
ambassador for financial services of the city. So, I spend approximately 80 days 
a year travelling the world. I will have visited 26 countries by the time I have 
concluded my annual appointment. It’s traditional that we always come to the 
Gulf. Every year, the Lord Mayor comes to one or more countries in the Gulf and 
it’s part of the constant relationship that we’ve had with this area over many 
centuries. <br>
<br>
The United Kingdom has had close commercial and strategic interests in the Gulf, 
and over the years, of course, many of the Gulf states have been regular 
investors in business to the UK. So, there is a familiarity with what we do 
here. Financial services are, of course, what I am mainly engaged with. So, all 
the topics are centred around the banking, insurance and wide ambit of financial 
services. I would be, therefore, meeting the central banks, economic ministries, 
the treasuries. In my capacity as Lord Mayor, I actually carry the status of an 
Ambassador when I am overseas and so it is quite common for me to meet the Head 
of State as a courtesy of course.<br>
<br>
The Lord Mayor comes as a minister of State effectively and that means that the 
foreign office and therefore the embassies in the country are very supportive 
and are very much responsible for coordinating the planning the tours and 
introductions. That inevitably means that some of the meetings we have are of a 
goodwill nature. They are not all business, but some are often to just build 
relationships, allow us to have access and to explain to a large extent what we 
do in the city of London. <br>
<br>
I should perhaps for clarity say that the role that I perform is quite different 
from the role that is performed by the Mayor of London. It’s a role that is 
often confused but that role focuses administratively on looking at road 
transport, supervision of the fire and police and so forth. Oddly enough, as a 
Lord Mayor, I am also the head of the Municipal Authority of the City of London. 
So the square mile—which is more than the square mile I should say—is under the 
control of an independent municipal authority called the Corporation of London 
of which I am the Mayor and, therefore, I represent it officially where 
appropriate. I am responsible for acting as a diplomatic host. <br>
<br>
So, every time there is an official State visit to the UK by a Head of State—if 
His Majesty was to visit from Oman officially, one of the events would be a 
banquet that I would give in his honour. I receive each week many overseas 
visitors ranging from ministers to chairmen and governors of Central Banks. I 
meet a lot of ambassadors who come in routinely and of course I meet businesses 
and trade delegations. So, I act as a host as well as a salesman if I could put 
it that way. <br>
<br>
The Corporation of London, itself is a big administrative authority – it’s 
responsible for the square mile of the city, which means that it particularly 
tailors the services to meet the needs of the business community. So, it 
encourages administration of the development offices, good communications, high 
standards of infrastructures, the restaurants and shops and all of that. 
Moreover, we also have some very unusual additions to this. We are also 
responsible for the river and, in fact, one of my titles is the Admiral to the 
Port of London. So, I am not just a Lord Mayor, but an Admiral as well. <br>
<br>
If you want to add further titles, I am the Chancellor of a City University. So, 
I have an academic institution to support and, finally, I hold a very unusual 
title of being the Chief Magistrate. The Chief Magistrate is a very ancient 
office going back to the Roman times and that means I extend my jurisdiction to 
the legal world and in particular, I serve both as a magistrate and as the 
senior magistrate for what we know as the Central Criminal Court. It is partly 
ceremonial, but it is nevertheless within the Corporation’s role and in fact my 
two sheriffs—one of which is here—actually are there each day responsible for 
the administrative side of the Central Criminal Court. So, that’s wearing my 
Municipal hat. <br>
<br>
<img border="1" src="noble1.jpg" width="200" height="192" align="right"><b>What 
do you aim to achieve during your overseas visits?</b><br>
One of the key elements of an overseas visit for me is to come away with an 
extensive impression of what’s going on in a different country, and that’s not 
just an overview. We prepare extensive reports. As we return back to the United 
Kingdom, we call together the businessmen in London in a round table conference 
– 70-80 people would come and we would present to them the opportunities that we 
identified in Oman. Clearly, business people in any walk of life—whether it be 
financial services or manufacturing or whatever—always go towards where it is 
easier to do business. <br>
<br>
So, if the message we convey back is really that we’re not totally welcome in a 
given country – the legislation is difficult, establishing the conduct of 
business is difficult – but if you go somewhere else, like Oman, where it’s an 
open, liberal society, that’s where you can do business easily – that sends a 
very strong message. Of course, it also means that we have a job to do in 
countries where conditions are difficult because, clearly, we wouldn’t want to 
ignore such countries. <br>
<br>
What we try and do there is to evolve discussions between our specialist lawyers 
and specialist accountants with a government agency to see if there is guidance 
and improvements if we could get to put those particular countries onto a 
footing that would be accepted internationally. You would probably better 
recognise this in free trade talks, free trade discussions, and WTO chapter. In 
a sense, that does extend in international services too. It’s as countries 
developing that international image adopt the new regulations, the new way of 
doing business that foreign investors become more comfortable. <br>
<br>
<b>What in specific are you looking to achieve with this visit to Oman?</b><br>
The primary role on this occasion is not only to familiarise myself with the 
Omani financial community, but to engage my delegation in direct discussions 
with business partners here to see if they can then further their relationships 
and to develop business contributions that both the city financial and Oman can 
offer. That falls into two categories – one, looking at investment into Oman and 
looking at the opportunities for investment here which obviously focus or have 
focused traditionally on the oil &amp; gas industries, but have now extended to the 
development of infrastructure, the care of SME markets—which is a downstream 
market of the oil &amp; gas industry—which inevitably will develop as time goes on.<br>
<br>
And two, to look in specific terms as to what we can do in financial services – 
so can we participate as banks? Can we develop links in the insurance industry? 
And also to deepen the financial services generally, the area of savings and 
general advice. These are all activities that go on at the moment of course in 
Oman, and some are done directly by some of our major international banks 
operating here like HSBC and Standard Chartered and, of course, domestic Omani 
banks also do them extensively.<br>
<br>
<b>Who have you met so far and who all will you meet during this visit?</b><br>
I have had just one business day here so far – I was interested in the economy; 
so, it involved talking to the Industry Minister. I was interested in manpower 
because, clearly, the employment of Omanis is going to become very important 
here. The demographic structure is 75 per cent of the population is under the 
age of 25. A huge amount will come into the workforce in the coming few years 
and I was, therefore, interested to see how we could participate in creating 
opportunities for employment of those students. I also went to see the Central 
Bank and those were the three important meetings so far. I will also be seeing 
the capital markets, stock exchanges, and other ministries associated with the 
economy. We never do focus on areas such as oil and gas industry, and so in that 
sense it’s not relevant.<br>
<br>
<b>How attractive do you find Oman as a business destination?</b><br>
One of the features is that Oman is an open and liberalised economy and it would 
welcome participation by foreign investors including ownership up to 75 per cent 
of banking institutions. And as yet, only fairly minor stakes have been taken – 
therefore, opportunities to look closely to how that integration could be 
deepened. One of the key requirements for Oman is to raise experience, skills 
and the innovation of new products; so, this cannot be done by partnership with 
international banks. <br>
<br>
The City of London is renowned in a sense for innovating new financial products, 
and these gradually spread out across the world, areas of savings, areas of 
managing risk by using derivative products, hedging products – not necessarily 
suited at this moment for Omani businesses but you have to begin the process of 
gradually building the experience. And that’s what we have been spending most of 
our time looking at and discussing.<br>
<br>
<b>What is your opinion of the pace of development in Oman?</b><br>
The world is a big place, and to a degree there is only limited amount of 
capital and resource. Obviously, in a country like Oman rich with petroleum and 
gas, there is naturally an interest in doing that. But on the back of it comes 
the scope for developing ports, developing all the infrastructure that supports 
the oil and gas development as well. One can see that Oman is being governed in 
a way with a degree of cautiousness, which is very necessary – you can make 
really almost too forward a step if you rush at things. <br>
<br>
Much the same approach is being adopted on tourism, where one can see that Oman 
has huge attractions for foreign tourists. Clearly, it has the historical and 
topographical framework – the forts, the mountains and the ocean. These are 
clear ingredients that can be developed as tourist attractions. Again, it needs 
to be done at a pace where the quality of the services and the attractions are 
consistent with the sort of spending power that one wants to get. That again 
means that you have to build the hotels, train the operators, provide the 
ancillary related facilities – all of that to a country like Oman is relatively 
new, and it means that the progression has to be supported by experienced 
organisations. <br>
<br>
I have already seen enormous hotel development. And I can see that the momentum 
is gathering very rapidly. I was pleased to learn that cruise liners ply 
regularly and they are good business as long as you can get the quality cruise 
lines and passengers to come and ensure they spend money. But you need to build 
the infrastructure to support that type of tourist traffic – if you have to have 
luxury shopping because that’s what they want, then there has to be those 
opportunities.<br>
<br>
<b>You earlier mentioned that you were interested in manpower…what role do you 
think the City of London can play in this regard?</b><br>
Clearly, one key area is financial training, and one of our delegate members is 
in fact from the city university – CASS Business School. Now, his background is 
to join up training courses with universities and other institutions and such 
negotiations are fairly routine. One of the reasons for this is that it is 
becoming very expensive to send students directly to the United Kingdom. To do 
an MA or BSc course will cost you a lot of money – and if, therefore, you can 
effectively joint participate with a local university or training college, you 
can actually set up that training program so that it can be delivered in the 
country concerned. <br>
<br>
It’s been made fairly clear that whilst existing arrangements do exist, more 
training would not come amiss, and indeed only now in the United Kingdom are we 
starting to develop the standards of competency necessary in financial services. 
We’ve had it for years in obviously the legal profession and the accountancy 
profession, but in the wide range of financial services, there’s been a lot of 
training, a lot of courses, a lot of institutes offering courses, but we haven’t 
given them comprehensive accreditation and that is going to happen over time.
<br>
<br>
The aim behind this is to raise the standards of corporate governance so that 
businesses have a uniform international standard of financial reporting in 
accountancy terms – disclosure of having conducted business, disclosure on 
trading, for example stock exchanges… so you don’t run the risk of insider 
trading and these standards are becoming generally understood in major financial 
centres.<br>
<br>
But are not always necessarily understood in developing markets. So, part of 
this whole program of training is to embed these principles of good corporate 
governance. Interestingly, there have been discussions while we’ve been here 
about Corporate Social Responsibilities (CSR), whereby corporate businesses 
utilise some of the resources—people resources—to help train and help mentor new 
and developing businesses. <br>
<br>
I went to have a look at the concepts of the Business Park (KOM); the 
incubator-type development of new enterprise and this is where mentoring—CSR 
programs—can fit in very well. The United Kingdom has a lot of experience in 
this area and whilst Omanis have had discussions here with organisations like 
the Princess Trust and other groups, it hasn’t become a fixed feature of our 
business environment – so that’s an area we believe that can be developed quite 
extensively. <br>
<br>
<b>How does the City of London undertake such efforts in the UK and how can that 
be adapted in Oman?</b><br>
The traditional ancient guilds of the City of London—known as the livery 
companies—were the trading bodies of many centuries ago, responsible for 
business trades in the city those days – the wool trade, the spice trade, 
vegetables and fruit trade etc. These were all the guilds that represented those 
industries and there are now 107 of them, and they include today information 
technologists, tax advisors, accountants, solicitors and so on. <br>
<br>
These guilds are groups of people that provide their services in a form of 
aiding and developing their industry and it’s all done for no cost. It’s like 
free consultancy – they pay for schools, they provide capital for training, they 
give money charitably to support their trade activities. These guilds are very 
generous – some of them are quite wealthy because they own a lot of property and 
they are part of the civic background of the City of London and I interface with 
them regularly. <br>
<br>
Even here in Oman, we’ve been talking about levels of qualification that we call 
City in Guilds qualification. These are qualifications of a practical nature – 
everything from planning to electricity etc. The qualification that you get by 
establishing your training institute that can deliver these gives you a 
professional standard equivalent to what you might get in an academic university 
and one of the things that we feel we could potentially assist in academic and 
training institute to establish that element of qualification. <br>
<br>
So, if you are an Omani and you have a qualification of a very good plumber or 
as a very good electrician, you can raise your salary level by the quality of 
service that you give. We were discussing the issue, for example in the United 
Kingdom, the construction industry is a very highly professional business, and 
it’s expensive. The people who work in that industry get paid very well whereas 
in other countries it isn’t so highly regarded as a quality profession. By 
raising the standards, by raising the qualifications, you push the standard of 
living up and the salary up. <br>
One of the elements that we’ve been told quite regularly today is the need to 
develop what we call the middle-class to build the structure of citizenship so 
you have an affluent middle class. With that middle class, you get savings, you 
get higher salaries and it builds the society around it – the retailing and all 
the infrastructure that comes with it. So, you’re raising the standards really 
for everybody. <br>
<br>
<b>Will that be your advice to Oman as well?</b><br>
The government here is very keen to do it and a lot of the infrastructure that 
it is building is to develop just that level of society. That comes with 
education, it comes with better health, it comes with all the ingredients that 
raise the standard of life and expectancy.<br>
<br>
<b>What else would be your advice for the Omani businesses and the government?</b><br>
I think the key element from my point of view is to work as closely in 
partnership with the financial institutions in the United Kingdom. That is very 
much an Omani background. Traditionally, Oman has been a trading nation; lot of 
their emphasis has been towards Pakistan and India, which is natural as it is a 
trading route. <br>
<br>
But London and the links that there have been with the United Kingdom have been 
equally very deep for a long time. We have been very much engaged here and 
assisting in the oil and gas industry and in the defence industry and we would 
like to see the relationships in banking and other commerce as strongly 
developed as possible. That’s the broad message. It’s the same message that we 
echo around the world I have to say but it’s very special to Oman because we 
have such a historical link of some 350 years.<br>
<br>
<b>What business opportunities have you identified so far?</b><br>
Very clearly, the interest in involving joint participation in Omani banks. 
There is no doubt a keenness and an openness to allow foreign investment 
directly into banks – the law permits shareholdings up to 75 per cent. The 
benefits of doing that would be two-way: firstly, a foreign investment 
group—whether it be a bank or otherwise—would have access to a good network of 
existing bank customers. Secondly, would enable the domestic banks to gain 
experience and have the benefit of business development introduced to their 
activities. <br>
<br>
One area here that has really got going is the area of investment in development 
banking. Investment banking in a sense comes to a more sophisticated environment 
and gradually there is going to be a need for this – assisting in the areas of 
developing the capital markets, the bond markets, the use of derivative products 
and risk management tools and more particularly to stimulate the areas of direct 
investment project capital and, ultimately, merger &amp; acquisition business, 
building in a sense the more sophisticated financial markets that we are used to 
in London. <br>
<br>
There’s no intent by the Omani government to turn Oman into a financial centre. 
This is not a Dubai – and it would be very right that it should not be a Dubai. 
I think the truth is that only in certain geographic areas of the world is there 
scope for big clusters of financial communities – and that’s not an Omani 
objective. I have already referred to financial training as an opportunity and 
it’s clear that in the educational field, there’s significant area of 
cooperation. <br>
<br>
But perhaps the biggest area—and an undefined one—is the development of SME and 
diversified new industries – in the areas of IT, if we could encourage 
businesses to look at Oman as a logical centre. One of the elements of this is 
the ability to have a resource of trained personnel, trained staff. I wouldn’t 
advocate low margin things – you need to have high margin profitability here and 
therefore it’s not a centre where you would expect to see low-paid personnel 
employee. What you want is well-paid, well-skilled staff giving you levels of 
standard and service which make it competitive internationally. So those areas 
need to be identified. <br>
<br>
Some of them will evolve naturally from downstream development of the oil &amp; gas 
industry. But others need to be created to a degree by persuasion. So, offering 
good accommodation, office facilities, good communications, and good leisure… 
people like to be able to come to an area where there is relaxation and they 
enjoy living. It’s not much fun living in a place where there are no restaurants 
and no social life, no sports facilities. This is one of London’s really big 
attractions. <br>
<br>
If you talk to business people as to why they like working in London, it is the 
cultural lifestyle that appeals and the very fact that the English environment 
is welcoming –multi-lingual, no limitations on where you come from and that’s 
not dissimilar to Oman, but clearly one has to be cautious about granting visas 
just willy-nilly to everybody. It (Oman) can’t afford to allow a high percentage 
of foreigners working here and the Omanisation programme to ensure that 
businesses have the right balance between a foreign participation and national 
participation is exactly appropriate and we’ve been looking at how that scheme 
operates and understanding the reasons behind it and that makes a great deal of 
sense. <br>
<br>
So, at the moment those are the identifiable areas. My colleagues in the 
delegation look individually to see what opportunities they can build, and I’m 
quite sure they have come away with some good thoughts. <br>
<br>
<b>How many members do you have in your delegation?</b><br>
We have a mixed delegation. I am roughly on a 2-week tour of countries in the 
Gulf. In the current time, we have 12 members in the delegation. But I have also 
got some supporting colleagues. One of them is my sheriff – now the Sheriff of 
the City of London is perhaps an unusual title but in fact they are always 
businessman and my sheriff on this occasion is an accountant from an 
international accountancy firm. I also have the support of the British Embassy 
here. <br>
<br>
As Lord Mayor, I lead a delegation comprising of bankers, educationists, people 
with experience across a wide range of disciplines like insurance and asset 
management. The delegation is made up of may people who are regular visitors to 
this part of the world, so this is not necessary the first time indeed. One 
member of my delegation actually came here first some 50 years ago, which is a 
very long time. The reason for that is they share great friendships with the 
Omani people. There know many of the ministers and many of the businessmen here.
<br>
<br>
I think like any business relationship, it needs to be cemented over a number of 
years. Of course, good relations mean good prices, competitiveness. But it does 
enormously helps to have that consistency of knowledge. So, the delegation this 
time is very experienced and goes with me to meet the ministers and the business 
leaders and has the opportunity of putting questions. It isn’t so much of a 
dialogue that you’re trying to test where the sensitive areas are. You are 
trying to get an overall consensus of what the atmosphere is about and, frankly, 
I have to say that we find no difficulty in Oman – the environment is extremely 
liberal and tolerant to financial services. <br>
<br>
In countries where there are difficulties, we make it pretty clear – we say that 
standards of law are not up to scratch or the transparency of doing things is 
not obvious. We make it quite plain that unless those standards are improved, 
they won’t get the level of foreign involvement that they are perhaps seeking. 
So, it’s a stick-and-carrot approach, really.</font></p>

