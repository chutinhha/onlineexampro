<img border="0" src="profile1.jpg" width="62" height="76" align="left" hspace="5"></font><b><font face="Verdana" size="3">W</font></b><font face="Verdana" size="1">hen 
asked to comment on the performance of BankDhofar in the year 2004, Ahmed Ali Al 
Shanfari, CEO of the bank, did not require too many words to describe. All he 
said was ‘Excellent’. BankDhofar has been riding high ever since 1995, the year 
they began setting records. Even when the stocks came crashing down in 1998, 
BankDhofar’s graph did not vacillate much, which was not so in the case of the 
other banks. And the year 2004 was no different when it came to profits. <br>
<br>
The bank’s total assets grew by 16.5 per cent in 2004. While its total assets 
reached RO552.2 million at the end of 2004, it was RO474.1 million in 2003. The 
profitability indicators also recorded significant growth, as net profit grew by 
10.0 per cent from RO10.2 million in 2003 to RO11.2 million for 2004.<br>
<br>
The loans and advances made to customers grew by 10.7 per cent and reached 
RO406.5 million at the end of the year. Growth was also recorded in customer 
deposits. It went up from RO358.4 million in 2003 to RO421.1 million at the end 
of 2004, which is a 17.5 per cent increase.<br>
The shareholders’ equity increased from RO63.1 million in 2003 to RO67.8 million 
in 2004. The net assets per share increased to RO1.615 at the end of 2004.<br>
<br>
<b>Starting on a high</b><br>
The first quarterly results for 2005 only reaffirms the bank's position as one 
of the leading local financial institutions in Oman. The net profit of the bank 
for January, February and March 2005 was RO3.31 million compared with RO2.81 
million for the similar period last year, a growth of 17.8 per cent. <br>
<br>
The total assets at the end of March 2005 reached RO541.1 million compared to 
RO484.2 million at the end of March 2004, indicating a growth of 11.8 per cent. 
Net Loans and Advances to customers also increased from RO366.6 million as on 
March 2004 to RO 417.4 million at the end of the current quarter. The customer 
deposits grew by 12.6 per cent at the end of March 2005.<br>
<br>
Shareholders’ equity increased from RO65.9 million at the end of March 2004 to 
RO71.1 million at the end of March 2005. <br>
<br>
The profitability indicators for the first quarter of 2005 show that profit from 
operations reached RO3.76 million compared with RO3.59 million for the first 
quarter of last year. <br>
<br>
<b>Achievements and targets</b><br>
&quot;Most of our targets for the year 2004 were achieved. The BankDhofar call centre 
was introduced to our customers through a soft launch. We will be introducing 
this facility formally over the next few weeks. We have also introduced the 
facility for customers to pay their bills either through our ATMs or tele-banking”, 
says Al Shanfari.<br>
<br>
The bank's training centre, which imparts training to the staff in various 
fields, conducted over 200 courses in the last one year, a major achievement in 
terms of employee skill enhancement. <br>
<br>
One of the bank's plans that could not be completed was the opening of the 
investment brokerage and financial institution department. &quot;We will be launching 
this department this year. Most of the staff are in place, the infrastructure is 
being set up and we hope to have this department functional in the next couple 
of months,&quot; says Al Shanfari. <br>
Among the other initiatives of the bank for the year 2005 is the introduction of 
privilege banking. &quot;We will be launching privilege banking shortly. We are 
hoping to offer better banking facilities to our customers with the introduction 
of such services,&quot; says Al Shanfari.<br>
<br>
<b>Looking ahead</b><br>
Extending support to reliable and enterprising entrepreneurs is one of the 
objectives of BankDhofar. But Al Shanfari feels more can be done to propel the 
growth of many sectors in Oman. &quot;We need to look at new avenues for growth. To 
achieve this, investors need to put money into new businesses or sectors. There 
are also opportunities to improve sectors that are already doing well like 
property and communications. If we are convinced about a project or plan, we are 
ready to finance it and make it a possibility,&quot; he says.<br>
<br>
The CEO sees 2005 as another year of success and achievements galore for 
BankDhofar. &quot;We are more aggressive this year and our services are constantly 
improved upon. We strive to keep the customers happy and satisfied. The quality 
of our services, our products and the trust people have in us has been our USP. 
And that's what is going to help us set records in 2005 too,&quot; says Al Shanfari. </font></p>

