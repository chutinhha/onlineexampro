W</font></b><font face="Verdana" size="1">e’re living in a world obsessed with 
the lifestyles of the rich and famous, fashion, sports, the mobile phone and the 
car. Today, nothing’s considered taboo and everything is open for discussion. 
It’s a world that’s being shaped by a younger generation, folk with different 
tastes and aspirations – a slice of society that’s demanding more. Who are they? 
We’re talking Generation Y (aka Gen Y’ers) – the generation born in the 1980s 
that are now entering the jobs market and higher education. The term Generation 
Y was coined in 1999, as ad execs, fashion houses and Hollywood tried to take 
the pulse of the lucrative teen market.<br>
<br>
Generational historians Neil Howe and William Strauss gave the concept added 
currency with their book Millennials Rising: The Next Great Generation Rising. 
Given Oman’s&nbsp; demographics, Gen Y’er’s are a massive market and an audience 
that banks, insurance firms, publishers, airlines, telcos, colleges through to 
manufacturers should be focusing on and talking to. But who are they, what are 
they like and how are corporates connecting with them? <br>
<br>
<b>A Savvy, Upbeat Crowd</b><br>
With both parents working and more disposable income than previous generations, 
Gen Y’ers have a distinctly practical view of the world and it’s a generation 
that has grown up with the Internet. Indeed, research indicates that Gen Y’ers 
are fully involved in family purchases, from supermarket shopping, holidays, 
music, cars, to Thursday night’s take-away choice. At 18, they’ve five-year 
plans, most expect to have careers and are already thinking about home 
ownership. They’re also looking at how they’ll be balancing their work and 
family commitments. <br>
<br>
Let’s be clear, this is a pragmatic, well-balanced group. It’s also the first 
generation that’s completely comfortable with IT. ‘‘Technologically, this 
generation is going to make Gen X’ers (1965–75) look decidedly slow,’’ says 
Mohammed Al Ghassani, Executive Vice President, PEIE and former Director of 
Knowledge Oasis Muscat. “Just think about it, with the pace and spread of ICT, 
in the next few years, Gen Y college students will be producing university 
papers with full-motion video.”<br>
<br>
<b>The Gen Y Psyche </b><br>
Gen Y’ers are less innocent and insulated than their parents – and because of 
their exposure to mass media, they know more about global politics, 
international trade and the Italian football transfer market than their parents 
ever did. Gen Y’ers will never know a world without computers. Global 
conversations over the Internet will bring distant cultures closer together. 
What’s more, they won’t even realize how remarkable that is. <br>
<br>
This generation views computers as basic equipment, like pen and paper, not 
something to be feared. Using technology for instant video communication and 
controlling the functions on the fridge will be commonplace for this generation. 
But given the make-up of today’s world, Gen Y’ers are pressed for time, their 
afternoons and weekends are taken up with football, tennis, swimming and karate 
lessons. They’re activity stretched. <br>
<br>
As Gen Y’ers begin to settle down, start families and confront the challenges of 
parenting and career juggling, experts believe they’ll bring different demands 
and attitudes to the workplace. They’ll work more on their own terms and their 
command of technology and early experience of affluence will put them in a 
unique position to negotiate those demands. This is a generation whose career 
choices and behaviour are driven by their quest for opportunities to play 
meaningful roles in the workplace, they won’t join a firm or government 
organization because they have to, but because they want to, because something 
significant is happening there. <br>
<br>
<b>Gen Y &amp; Marketers</b><br>
What does all this mean for Oman-based marketers? In brief, if you want to reach 
tech savvy, worldly-wise Gen Y’ers then you need to craft your products/services 
and pitches to be more realistic. For example, MTV switched the emphasis of its 
weekly fashion show from celebrity lifestyles to practical information, with 
segments on decorating bedrooms and buying clothes.<br>
<br>
Given the amount of media and marketing saturation Gen Y’ers have been exposed 
to, marketers are making their campaigns more subtle and local in flavour. A 
growing number of blue chips, including Universal Studios, Coca-Cola and 
McDonald's are using street teams. Street teams consist of young people that 
hang out in clubs, parks, on the beach and shopping malls talking to teens about 
everything from fashion to finance, trying to pinpoint trends as they emerge. 
Other marketers are sponsoring community events or give away coupons and 
T-shirts at concerts and sports events. Publishers are handing out sample 
chapters from new teen books at cinemas. The psychology behind this is to let 
Gen Y’ers stumble onto the brand in unexpected places rather than hitting them 
head-on.<br>
<br>
<b>Who’s Doing What?</b><br>
Who’s tapping the Gen Y market? Publishers are certainly active - in the US, 
annual sales of young-reader books has more than doubled to RO550 million, since 
1987. Clothes - from Ralph Lauren to Gap are targeting the Gen Y crowd. Hotels 
are offering kids’ programs. Shopping malls, furniture stores and even 
supermarkets are providing on-site crèche areas. Restaurants are putting out 
crayons, changing tables in bathrooms and offering more take-out services, all 
to serve families with kids.<br>
<br>
With the oldest Gen Y’ers now in their mid-20s, car manufacturers are finding 
themselves grappling with a new generation of buyers who have strong opinions. 
In this regard, Toyota was out of the blocks fast with the Echo - low emissions 
and a price well below the Corolla, it’s aimed straight at Gen Y’ers who are 
buying their first cars. In the US, General Motors has put together a task force 
that’s dedicated to studying Gen Y’ers ideas and opinions on car design. The 
firm brings teens into car clinics where researchers probe their opinions on 
current models and prototypes of future vehicles. Gen Y’ers are also driving the 
educational-software industry, which has grown to a RO300 million business from 
practically nothing in 1990. Today, we’ve electronic games designed to help 
infants as young as six months learn to identify numbers, shapes, colours and 
body parts. <br>
<br>
Have you noticed how computers, mobile phones and other hi-tech kit are looking 
less industrial and more funky? This is a result of manufacturers looking to 
attract younger buyers. Apple Computer is a great example of this, with its 
rounded, space-age-looking iMac computer. <br>
<br>
In short, if you’re interested in marketing to Gen Y’ers then you may want to 
think about these three points before you start spending.<br>
<br>
•<b> Respect:</b> You need to care about their opinions. Gen Y’ers are a lot 
more respectful than the generation that came before them. You can't make them 
feel small or treat them as if they don't know anything. And the worst thing you 
can do is act like something you're not. Brands need to go where Gen Y’ers are, 
not just pick a fashion statement, put it on TV or a newspaper and wait for them 
to walk in – it won’t happen.<br>
<br>
•<b> Options:</b> Don't choose actors or models who you think represent 
Generation Y. Gen Y consumers want to see an array of personalities. Variety is 
completely acceptable with them.<br>
<br>
•<b> Quiet:</b> It's true that Gen Y consumers like to discover brands on their 
own. That doesn't mean that they don't want to be marketed to. But it does have 
to be on their terms and in places where they hang out. n</font></p>

