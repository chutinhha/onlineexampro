<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">
Smelting success<br>
<br>
</font><b><font face="Verdana" size="1">Vicky Kapur talks to Tony Kinsman, the 
CEO of Sohar Aluminium, about the status and expected benefits of the project 
that is set to contribute more than 2 per cent to the country’s GDP after 
start-up. Excerpts from an exclusive interview:<br>
</font></b><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" size="1">
<img border="1" src="tony.jpg" width="120" height="127" align="left" vspace="2"></font><font face="Verdana" size="3">T</font></b><font face="Verdana" size="1"><b>ell 
us about the Sohar Aluminium project…<br>
</b>Sohar Aluminium will be a Greenfield smelter – a rather unusual project 
these days. Most of the new aluminium capacity is brought on by additions to the 
capacity of existing smelters. So, the advent of a Greenfield smelter is not so 
common and represents an exciting challenge for the company and for Oman. It is 
the sort of challenge that will attract some of the best people and 
organisations in the industry to participate in the project.<br>
<br>
<b>What is the total project cost?</b><br>
The project cost is presently around $2.2 billion (RO846 million).<br>
<br>
<b>When was the current phase of the project commissioned?</b><br>
Sohar Aluminium has been in development for a long time and there have been a 
number of phases in its development. There have been partners in the past that 
have fallen away, but the latest phase of development of the project has been 
progressing steadily for some time now, and is well advanced.<br>
<br>
The current phase came together in the form of a Memorandum of Understanding 
between Oman Oil, Abu Dhabi Water &amp; Electricity Authority (ADWEA) and Alcan on 
June 30 last year. This in fact also was the time that I agreed to become the 
CEO of a new company. Sohar Aluminium Company LLC was, in effect, formed on 
September 15 last year and a formal Shareholders Agreement between the three 
shareholders was signed on February 23 this year. <br>
<br>
The EPCM contract for the smelter is presently under negotiation with a 
preferred bidder, namely Bechtel, following a competitive bidding process 
between major companies that have, in recent times, managed the construction of 
similar large aluminium smelter projects. The Bechtel team has recently 
completed the EPCM contract for the Alba Line 5 expansion in Bahrain, and the 
same team will come to Oman to help build our smelter.<br>
<br>
<b>How is an EPCM contract different from an EPC contract?</b><br>
This is not a lump sum contract where the owners place one single contract with 
a main contractor, who, for its own account, and at its own risk, places other 
contracts with smaller contractors and suppliers. <br>
<br>
In an Engineering, Procurement and Construction Management (EPCM) contract, the 
EPCM contractor will manage, on behalf of the owners. Many smaller supply 
contracts including lump sum and supply packages. It’s a typical form of 
management contract for an aluminium smelter, and requires considerable support 
and involvement from the wwners and the shareholders during construction. <br>
<br>
<b>How is the shareholding of the company structured, and what is the value that 
each of the strategic partners brings to the table?</b><br>
<br>
Clearly, each one of them brings something special to the project. Oman Oil has 
a 40 per cent stake; they are essentially the project developers and bring to 
the project a good knowledge of project development and of “project financing”, 
which is the basis on which the smelter will be funded. ADWEA, also with 40 per 
cent, has expertise in the development of power stations, having privatised a 
substantial portion of the Abu Dhabi Power Supply System and having developed 
some new power stations in Abu Dhabi. So, we are using of their expertise to 
help us develop our power station. <br>
Alcan will take a 20 per cent stake in the first potline, but will have the 
ability to invest up to 60 per cent in the second potline. Alcan, the world’s 
largest primary metal producer, will be the provider of the AP 35 technology, 
and in addition will have a long-term management support agreement, a long-term 
metal offtake agreement, a long-term alumina supply agreement.<br>
<br>
<b>How does the shareholding structure of the project lend itself to the 
management structure of the company?</b><br>
Sohar Aluminium is a standalone project and operating company, which has its own 
management. I am the CEO, and I have my management team in place, which will be 
fully supported by the expertise and experience of the respective shareholders.<br>
<br>
<b>Do you represent any of the shareholding partners?</b><br>
While I don’t represent any of the specific partners, I have a responsibility to 
all of them and to their company. I have joined Sohar Aluminium independently of 
each of the shareholders, and also some of the senior executives are independent 
of shareholders. There are also a number of executives who have been appointed 
from within the shareholders, particularly Alcan, which has a management support 
agreement with us. <br>
<br>
I have been involved in the development of aluminium smelters for the last 15 
years. Six of the seven senior executives in our management team have all been 
involved in previous successful AP 30 projects somewhere in the world. I believe 
we have a very strong management team in place to lead the development of this 
new aluminium smelter.<br>
<br>
<b>Why has Oman chosen to develop an aluminium smelter industry?</b><br>
To give you a brief background, the Batinah region around Sohar has around 
600,000 people, about 30 per cent of Oman’s population. Almost two-thirds of the 
Omani population is under the age of 24 years. So, the Oman government sought to 
identify industries that will offer careers in skilled jobs for Omanis.<br>
<br>
If we look at the strategy for development of Oman, figures (the Vision 2020 
projections) show a declining dependence on oil—as reserves would have 
depleted—and a growing dependence on gas and manufacturing. Aluminium smelting 
offers an opportunity to develop in both these fields. And of course trade fits 
in there somewhere particularly when we move to downstream products. So, this 
gives a very viable basis for the strategy of the Oman government to support 
aluminium production as a sector for development. <br>
<br>
<b>With the joint capacity of Aluminium Bahrain and Dubai Aluminium currently 
standing at over 1.5 mtpa, and set to grow significantly in the coming years, 
along with the proposed smelters in Kuwait, Qatar and Saudi Arabia, the region’s 
dominance of the aluminium market is of course set to increase…</b><br>
The Gulf is a great area for expansion in the current development cycle of the 
aluminium industry. The region offers a good opportunity for new smelter 
development, particularly given the availability of competitively-priced 
gas-based energy, good infrastructure in the form of ports and roads etc. and 
reasonable construction and labour cost, all of which make the Middle East 
attractive for the development of new aluminium capacity. <br>
<br>
We are aware of a number of projects that are in development in the future – 
particularly Qatar, Iran and Saudi Arabia. Oman has a strategic location, which 
offers good access to markets of Asia and Europe. Additionally, Oman is a 
politically stable country, with a government that offers good support to the 
new development. <br>
<br>
<b>Do you expect the global demand to rise simultaneously with the rise in 
production in the Gulf?</b><br>
Well, certainly. Let’s take a look at the world industry, where the capacity is 
presently around the 25-million tonnes per annum mark and growing at an average 
rate of around 3-4 per cent per annum, i.e., growth of some 750,000 tonnes per 
annum, where just to meet such growth, two AP 30 pot lines a year, like ours, 
would need to be constructed. In fact, the reality is that around half of the 
new capacity each year is brought on by increased production from existing 
potlines in existing smelters. This growth is balanced by reductions of existing 
capacity as older, more expensive smelters either reduce capacity or close down. 
So, the world has a capacity of round about of one new potline every year. So, 
there is certainly space in the world demand. <br>
<br>
<b>
<img border="1" src="tony1.jpg" width="120" height="117" align="left" vspace="1" hspace="5">What will be the capacity of the first potline, and will there be more in the 
future?</b><br>
It will be one potline initially, with a capacity of 325,000 tonnes per annum. 
It’s intended to proceed with a second potline immediately following the 
successful commissioning of the first potline, which expansion we presently 
estimate to start around 2010. We have a high degree of expectation of 
proceeding to the second potline due to the fact that we have gas allocated for 
two potlines. <br>
<br>
<b><br>
How will the smelter be powered?</b><br>
This smelter will have a dedicated gas-based power plant, which is the preferred 
configuration for a new smelter. We’re looking at a 800-mega watt gas-fired 
power plant extending to 1400 mega watts for Phase 2. Importantly, sufficient 
gas is being allocated for two potlines for a minimum of 26 years. <br>
<br>
<b>Where does this gas come from? </b><br>
It comes from a combination of sources, but it is essentially from the main feed 
that goes into Sohar.<br>
<br>
<b>Where will the raw materials to feed the smelter come from? </b><br>
The Alumina is being provided by Alcan, and is likely to come from Australia. I 
say ‘likely’ because until we have the contract finalised, I can’t say 
definitely. We will soon be entering the market for other raw materials such as 
calcined petroleum coke and pitch.<br>
<br>
<b>What markets are you looking at?</b><br>
It is expected that, initially, the aluminium products will be exported, but in 
time, it’s expected that some downstream businesses would develop that add value 
to the pure aluminium, which will also contribute to the economy and to 
employment opportunities in the Sohar region. We want to really contribute and 
bring Sohar alive as an industrial area. <br>
Another important fact is that smelting has a high follow-on flow of jobs into 
the community and research from past projects in developing areas shows that for 
every single job in a smelter, there could be upwards of four jobs created 
outside, supporting the smelter. We think this will be a major contributor to 
the region as many of those jobs carry high level of skills for which 
substantial training will be needed. <br>
<br>
<b>What kind of technology will Sohar Aluminium employ? </b><br>
We will employ the AP 35 technology, which is a progression of the very 
successful AP 30 technology and in fact we will be the first commercialisation 
of the AP 35 technology in the world. However, there have been a number of cells 
operating of this configuration in France for a number of years, and it is only 
a marginal increase on the present capacity of a number of smelters. <br>
<br>
The AP30 is the Aluminium Pechiney technology, developed by Pechiney, which is 
now owned by Alcan. Alcan acquired the AP 30 technology with its recent 
acquisition of Pechiney in 2003. Sohar Aluminium was considering using this 
technology prior to this time as it is fully recognised as the leading aluminium 
smelting technology in the world. <br>
<br>
