<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">The Big 
‘B’</font></font><font face="Verdana" size="1"><br>
<br>
<b>Matt Harrison explores the nitty-gritty of branding and explains when, why 
and how to undertake re-branding</b></font></p>

<p align="justify"><font face="Verdana" size="1">There are as many definitions 
of branding as there are branding agencies and consultants. Your brand is what 
you stand for, and what you won’t stand for. It’s your company’s personality. 
It’s how you will and won’t do business. It’s the customers you seek and the 
ones you don’t. It’s how you treat employees, partners, vendors and customers. 
It’s the care that goes into your product or service. It’s your overriding 
principles and your diligence in adhering to them. In short, branding is the sum 
total of your values, as evidenced by how you deliver on those values, at every 
point of contact.<br>
<br>
Now, if you think about it, we’ve all seen oil companies, telcos, manufacturers 
and banks proudly telling us they’ve revitalised and redefined their brand. They 
release a new logo—generally a poorly designed one—and a meaningless slogan. And 
believe it or not, they’re genuinely chuffed with what they’ve got. More to the 
point, they’ve paid through the nose to get it. Listen up folks, a slogan and a 
logo don’t make a brand. <br>
<br>
<b>The background</b><br>
Originally, branding referred to burning one’s mark on bovine rears to help US 
ranchers distinguish among look-alike cattle but it was the American railroads 
that brought branding into the marketing lexicon. Railroads made mass 
distribution possible, which, in turn, made mass production viable. With mass 
production came copies, and with copies came the need for manufacturers to 
differentiate their products. They solved the problem by adding proprietary 
marks to their packaging and began referring to the practice as branding.<br>
<br>
To cut a long story short, branding worked. Consumers liked brands and it wasn’t 
long before brands became highly valuable assets. For example, after an 
unsuccessful foray as a women’s cigarette, Marlboro reintroduced itself as the 
cigarette for manly men. Powerful TV spots, road-side billboards and magazine 
ads featuring handsome cowboys puffing away on the cigarette quickly had any 
male smoker who wanted to look cool, smoking Marlboro, and the Joe Consumer was 
willing to pay extra for the privilege.<br>
<br>
Other brands without a readily apparent competitive advantage were quick to 
differentiate by image as well. Substance was optional. It worked for a while, 
but as brands and choices proliferated and Joe Consumer paid more attention to 
the benefit of a product, brand loyalty began to erode. Folk soon realised that 
buying Hunts, Heinz or Paul Newman’s tomato sauce had little impact on the 
outcome of their Thursday night pasta sauce. The unthinkable was happening, 
despite distinctive trademarks and carefully crafted images, well-known brands 
were becoming parity products. Even the cowboy lost his grip, and mighty 
Marlboro found itself doing the ultimate brand no-no and lowered its price to 
compete.<br>
<br>
<b>The road to Damascus</b><br>
For many companies, the branding process can be a real soul-searching journey, 
one that leads them to defining who they are, what they stand for and why 
customers should place their faith in their products or services. Brands are 
experienced through, inter alia, a company’s Website, letterhead, brochures, 
corporate film, office environment and the receptionist. The brand has to 
permeate every level of the organisation and every point of a customer’s 
experience to be effective. People want to connect with a brand, but for this to 
happen, the brand has to appeal on an emotional level.<br>
<br>
Your brand should be thought of as a set of values implied by your product, 
service or experience and not simply as a symbol, which is usually your logo. 
Your logo is merely the manifestation of the brand, the visualisation of the 
emotional reality. Indeed, all the values associated with the brand, good or 
bad, are brought to mind when the logo is seen. Technically speaking, a brand 
isn’t created by designers or other professionals – it’s created in the minds of 
customers, audiences and participants through experiences with the brand. 
Brands, therefore, live outside the company. Let’s be clear, what the customer 
thinks, does and ultimately buys is driven by perceptions. So how does an Omani 
company go about branding? What are the hoops they need to hold up and jump 
through? Here are some tips: <br>
<br>
<b>Who are you anyway?</b><br>
Investigate and research, talk to your competitors, vendors and customers. Find 
out how your brand’s perceived. What is its current position? Consider your 
brand’s strengths and weaknesses. What are the opportunities and threats? Also 
think about the strategic goals behind existing marketing efforts and who your 
primary target is and how they behave. Figure out where your brand stands before 
you opt for a new direction. <br>
<br>
Once you understand where your brand is and what it is to become, discuss with 
colleagues how to get it there. What tactics will help you achieve your 
strategic goals? What tonality and aesthetic is appropriate for the target? 
What’s your Brand Contract, the target’s “take away” after engaging the brand? 
And what needs to be done to continue the relationship once it’s begun?<br>
<br>
<b>Piece it together</b><br>
Answering these questions ought to give you a direction that can be pursued. The 
direction should take the form of a Concept Board and is the final step in the 
pre-design stage. The Concept Board should take your complex business strategies 
and mould them into a visual and verbal representation that triggers Joe 
Consumer’s emotive values. This is the litmus test against which all creative 
decisions are measured, keeping the work focused and on strategy.<br>
<br>
<b>The bit everyone likes</b><br>
Design is certainly the most difficult part, for many, it’s the most obvious; 
the best understood and needs the least explanation.<br>
<br>
<b>Roll Out</b><br>
Print it and (e-)mail it. Immediately after launch, begin to generate findings. 
From this valuable data will emerge more questions, which will continue the 
focus on the most efficient, effective way to meet brand objectives. No matter 
how much brand designers contribute, if a brand isn’t viable or sustainable, it 
won’t succeed. On the other hand, well-managed and well-executed brands that 
keep business objectives and target audience in mind will effectively capture 
Joe Consumer and maximise return on your investment.<br>
<br>
<b>Who brands wrong?</b><br>
• <b>Gap sends the wrong signals. </b><br>
The clothing store Gap abandoned what it was famous for—good value in 
traditional clothing—and lost core customers when it stocked up with trendy 
clothing, which didn't sell. Gap’s brand promise was muddled and mismanaged and 
now it’s suffering. <br>
• <b>Volkswagen - the car for 'the people' - not 'the very rich people'. </b>
<br>
Extending a product upward in price and prestige is much harder than moving 
downward. Taking a confirmed mid-range product brand and moving it into the 
stratosphere is a guarantee of one thing. It won't matter if the product is good 
enough to bear the price, because it conflicts with what customers believe it 
is. A RO17,000 price tag for a Volkswagen? That’s a hard promise to sell. <br>
• <b>Virgin on everything.</b><br>
A strong, memorable name is a powerful part of a brand. Put that name on strong 
products or services that are consistent with its image and it adds value. Put 
it on weak ones and it loses its value even faster. There are times that a brand 
just has to say 'no' to a brand extension. Weak brand extensions weaken the 
entire brand and that’s a terrible thing to do to a strong brand name. <br>
<br>
<b>Who Brands Right?</b><br>
• <b>BMW - the ultimate driving experience. </b><br>
When BMW makes a claim like this, it had better back it up with performance. 
What has BMW done? It has re-engineered its legendary 3-series and made it even 
better. Then it goes to the extremes of technology in its top-end 7-series. When 
they say 'the ultimate driving experience', that is the promise and BMW sales 
are strong because they are what they say they are. They’re keeping their 
brand’s promise. <br>
• <b>Apple - think different. </b><br>
Who would have believed egg-shaped computers in citrus colours? Titanium cases 
and movie-shaped screens on laptops? Clear plastic cube-shaped computers? Flat 
screens suspended on articulating arms from hemispherical CPUs? A totally new 
operating system and software - is Apple thinking 'different'? Looks like it - 
because that's its brand promise, and it’s keeping it.<br>
• <b>Ralph Lauren - enduring, consistency in style, quality and cachets. </b>
<br>
Ralph Lauren consistently uses its brand on products that match its image - good 
quality, comfortable, enduring and tastefully stylish. Ralph Lauren keeps his 
fashions on track because that's his brand's promise. <br>
<br>
<b>Changing times</b><br>
Times have changed. Linking a brand to a cowboy isn’t enough anymore. Indeed, 
it’s become more difficult to get Joe Consumer to part with his hard-earned 
Rials. Today, the practice of claiming to be unique without changing anything 
but your advertising isn’t enough.<br>
More and more Oman-based companies run campaigns telling us they’re different in 
the way they think, hire and behave. But when you visit them, you find a bank, a 
petrol station or a telco that looks, feels and acts just like it did before. 
This isn’t branding. It’s letting your customer down. Let’s be clear, Joe 
Consumer demands substance and rewards those who deliver it.</font></p>

