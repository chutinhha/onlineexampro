            <b>The outlook</b><br>
            The market has fetched abnormal returns in the first four months of 
            2005, and has come very close to its all-time high of 5000-levels, 
            reached way back in 1998. However, the bull run of 1997-98 was 
            different from the current rally because the market at that time was 
            driven by leveraged capital as a result of the margin and trust 
            accounts. This has, however, changed over the last few years, with 
            margin and trust accounts no longer existing. The only leveraged 
            capital existing in the current market is in the form of bank 
            lending, which is also regulated as per the Central Bank of Oman 
            (CBO) norms. Hence, the current market growth appears to be 
            originated from a real source of investible surplus in the market 
            and not buoyed by leveraged funding.<br>
            <br>
            The last few days of the period under review showed that the market 
