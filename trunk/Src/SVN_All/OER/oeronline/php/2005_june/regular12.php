            drop in Bahrain and Qatar markets, both in the process losing by 29 
            per cent and 20.4 per cent respectively. We believe that unlike its 
            regional counterparts, Oman has understated the price of oil prices 
            significantly in the current budget. Hence, the average oil price 
            which ruled at around $42.5/barrel for the first four months of the 
            year is expected to be maintained and average out at around at least 
            at $35+/barrel in the current year. This would indirectly improve 
            the oil revenues on the same production base by more than 50 per 
            cent. <br>
