<p align="justify"><font face="Verdana" size="1">
<b>
<img border="0" src="pers1.jpg" width="150" height="200" align="left" vspace="2" hspace="5">Entrepreneur par excellence, Shiekh Qais bin Salem Al Khalili talks with Anne 
Kurian about the innovative corporate culture he introduced at Al Khalili Group 
that put the company right on top</b><br>
<br>
It isn’t always that one comes across entrepreneurs who are unfazed by 
competition. Sheikh Qais bin Salem Al Khalili is one such entrepreneur who 
thrives on competition. The more, the merrier. It’s this attitude that Sheikh 
Qais believes has led his company to grow by leaps and bounds within a very 
short time. <br>
<br>
As the MD of the Al Khalili Group, one of the leading business firms in building 
materials and construction, Sheikh Qais brought far reaching changes in the 
company that placed him at the helm at the age of 27.<br>
<br>
This young entrepreneur was born during the beginning of the Renaissance era and 
is understandably proud of the fact. “I consider myself lucky to be born during 
this period, which marks the growth of the Sultanate,” says Sheikh Qais. “His 
Majesty’s vision and dreams for the country are the reasons we have been able to 
reach where we are. He came into power in 1970 and our business was set up in 
1974. He ushered in a new era of development that also helped our business to 
grow.”<br>
<b><br>
New kid on the block</b><br>
After completing his high school education in Muscat, Sheikh Qais went to the 
University of Florida to pursue higher studies in Business Administration. He 
returned in 1996 armed with not just a degree in business administration, but 
also brimming with new ideas and strategies to help run the family business 
better. <br>
<br>
“There were a number of businesses being run by the family, but the core 
business was of building materials and construction. My father was extremely 
busy handling all the businesses single-handedly and I decided to help him. I 
was not sure if he would trust a guy just out of college and had no experience. 
Handing over the reins was a risk not just for my father, but also me. All the 
other managers and directors then were experienced and over the age of 40 while 
here I was just 27 and a fresh graduate,” says Sheikh Qais.<br>
“But luckily for us, our father has always given us the freedom to do what we 
wanted, whether it was studies or career. He trusted me and gave me control of 
the company with no hesitation. It took me some time to understand the business, 
but within a year, the business started to thrive and I was made director of the 
company,” he says.<br>
<br>
It was not an easy task for Sheikh Qais to introduce the changes he wanted, as 
he was a new and young entrepreneur. The experienced hands in the company took 
time to adjust to the changes and ideas Sheikh Qais implemented, but it did not 
take him much time to prove himself.<br>
<br>
“It’s quite normal for the young blood to clash with the old blood because they 
had their own ideas while I had mine. But I felt that my exposure to the outside 
world during the four years I was pursuing my business degree helped me look at 
things in a different way,” says Sheikh Qais. <br>
<br>
Within a span of eight years, the company, which was making a turnover of $5 
million, today has an annual turnover of $45 million, a progress that was 
achieved by the vision of Sheikh Qais, who is also a member of the Chamber of 
Commerce. <br>
<br>
<b>Management mantra </b><br>
Although the business was doing well when he joined, Sheikh Qais wanted the 
company to reach higher levels. After taking over, he set about to introduce 
innovative management policies and a new corporate culture in the Al Khalili 
group. <br>
<br>
“In one of my business studies classes, we were asked to discuss about the 
reasons for the success of a company. Treating your employees and making them 
feel that they own the business will boost a company’s growth is what I told the 
class that day. After coming back, I put that vision into action. You have to 
make your employees feel they own the business and are an integral part of the 
company. <br>
<br>
“I held a meeting with my employees and told them to handle the business as 
though it was their own. I told my branch managers to take certain decisions on 
their own. They were given a free hand to run the business. If the department 
they were handling did well, they would be commended and if it went down, then 
they would be questioned. This corporate policy went down well with my employees 
and boosted our business,” says Sheikh Qais.<br>
<br>
A saying of Prophet Mohammed inspired the next initiative introduced by Sheikh 
Qais in his business. “Give the man his money before his sweat dries, said the 
Prophet. It had a profound influence on me and I felt that it was so true. Why 
should an employee wait for a year to get his bonus? Why not give it to him on a 
monthly basis? I knew that if an employee were recognised for his work on a 
monthly basis, it would only help him work better. So I introduced the policy of 
profit sharing on a monthly basis, depending on the employee’s work. It was like 
an energy boost for all the employees. They worked better and were happier,” 
reveals Sheikh Qais. <br>
<br>
“It was not only on the work front that improvements happened, but also in the 
lives of the employees. With this new policy in place, many of them had the 
opportunity to send their children to good schools and daycare centres and lead 
better lives,” he says.<br>
<br>
Many of the initiatives were risky, but Sheikh Qais studied the market and 
researched well before putting any idea into action. “There were many of my 
competitors who told me that I would go bankrupt when I introduced new ideas. 
Surprisingly, the people who dissuaded me in my business strategies were the 
ones who went packing from here when their businesses collapsed,” he says. <br>
<br>
Competition is another factor that has led this young entrepreneur to perform 
better and take his company notches ahead. “I love competition, “ he says 
zealously. “I like waking up to the news of new competitors or businesses in the 
market. It makes me run faster and perform better. But unhealthy competition is 
not for me. I like to be among competitors who drive me to achieve what I want,” 
he says. <br>
<br>
The fact that he welcomed competition and introduced new plans to counter it has 
boosted the business’ growth over the years. This was a fresh change to the 
otherwise competition-not-welcome policy of the management before Shekh Qais 
took over. “I have always wanted to be near my competitors because that’s one 
way to wake up my employees and get them to work better,” he says. <br>
<br>
Another feather in the cap for Sheikh Qais is the rise in the percentage of 
Omani staff in the company after he took over. “When I joined, there were just 
2-3 Omanis in the company. Today we have achieved over 50 per cent Omanisation 
in the company. I believe that the private sector has enjoyed many benefits from 
th<b><img border="0" src="pers2.jpg" width="155" height="165" align="right" hspace="5" vspace="2"></b>e government and it’s payback time. We do not have to wait for government 
orders to take on Omani staff. It’s something we can do on our own,” he says.<br>
<br>
The Khalili Group is now looking at mergers, another new initiative from Sheikh 
Qais. “There’s bound to be more competitors entering the market soon, and we 
need to be prepared. We are currently in talks with a few companies and might 
merge with a couple in the near future. We are also looking at the possibility 
of going public very soon as the businesses are doing extremely well,” he says.<br>
<br>
<b>Personal notes</b><br>
When Sheikh Qais is not working, he is pursuing his various interests, which are 
mostly outdoor-based. “I love playing volleyball and pool. Oman has such 
beautiful beaches that it’s hard to stay away from them. I go swimming and 
fishing regularly,” says Sheikh Qais. Apart from playing soccer and going biking 
with his friends, Sheikh Qais enjoys taking his family out. <br>
<br>
Travelling is another interest that Sheikh Qais has and even if it’s a business 
trip, he tries to spend time understanding the culture and people of the regions 
he visits. “Getting to know the culture and socialising with the people of a 
country I visit has always helped me in my businesses. There are many instances 
during business meetings when I have been able to connect with people easier as 
a result of the knowledge I have of different cultures. It helps in bridging the 
gap with my business partners,” he says. </font></p>

