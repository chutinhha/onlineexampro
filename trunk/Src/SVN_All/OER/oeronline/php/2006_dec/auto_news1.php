        <b>Going the Extraaa… Mile</b></font><p style="margin-left: 10px; margin-right: 10px" align="center" dir="ltr">
        <img border="0" src="autonews4.jpg" width="350" height="211"><p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
		<font face="Verdana" size="2">
        T</font><font face="Verdana" size="1">he ‘Shell Better Mileage 
        Challenge’, a unique competition that tested driving endurance and fuel 
        economy using Shell Better Mileage petrol created a buzz in Oman on 
        November 23, 2006. A total of 39 teams — each comprising two members — 
        representing various Ministries, corporates, media and leading 
        automobile dealers attempted to get the best mileage in their vehicles 
        using Shell Better Mileage petrol as they drove on an approx. 225 km 
        route starting from Muscat. <br>
        <br>
        </font>
        <img border="0" src="autonews5.jpg" align="left" hspace="5" vspace="2" width="150" height="123"><font face="Verdana" size="1">The 
        participants were flagged-off by HE Eng. Ahmed Bin Hassan Al-Dheeb, the 
        Undersecretary of Ministry of Commerce and Industry, from the Shell 
        Service Station at Al Ghubrah at 2 pm. Besides staff from Shell Oman and 
        other dignitaries, fuel economy enthusiasts Helen and John Taylor were 
        also present at the venue to give last minute tips to the participants 
        on how to achieve better mileage using simple techniques.<br>
        <br>
        Both Shell Super and Regular fuels in Oman are enhanced with the Better 
        Mileage formula offering customers the best value along with better 
        mileage and resultant cost savings, as Shell fuels come at no extra 
        cost. The Better Mileage formula is designed to also improve engine 
        efficiency and reduce emissions, thereby, having a positive effect on 
        the environment. “With the launch of the Better Mileage Formula in Shell 
        petrol, Shell is committed to help its customers get the most out of 
        their fuel, at no extra cost. The Better Mileage Challenge is one such 
        endeavour to help everyday drivers experience the advantages of Shell 
        petrol and proven Fuel Economy tips as demonstrated by the Taylors,” 
        said Faisal Al Hashar, General Manager — Retail Sales and Operation, 
        Shell Oman Marketing Company SAOG. “At the same time we wanted the 
        contestants to also have some adventure and excitement in their quest of 
        the ultimate better mileage honours,” he added.<br>
        <br>
        </font>
        <img border="0" src="autonews6.jpg" align="left" hspace="5" vspace="2" width="150" height="123"><font face="Verdana" size="1">Besides 
        the serious task of getting the best mileage out of their vehicles, to 
        add a bit of fun to the Challenge, participants stopped at three 
        ‘mystery’ pit-stops where they performed various tasks involving mental 
        and physical challenges before reaching the finishing point. Clues were 
        provided for teams to reach each pit-stop.<br>
        <br>
        The contestants were divided in four different categories on the basis 
        of the engine capacity of their vehicles. The winners in various 
        categories were selected on the basis of the fuel efficiency and not on 
        the time taken to complete the Challenge. All the contestants returned 
        good mileage. The OER team represented by Akshay Bhatnagar and TR Vinod 
        driving Kia Rio 1.4 L manual transmission managed an outstanding average 
        of 20 kmpl. The winners in different categories were:<br>
        <br>
        <br>
        Category A (1600cc and below) – Victor and Jessint with 22.35 kmpl<br>
        Category B (1601cc to 2000cc) – Jasim Al Maharbi and Ahmed Al Oufi with 
        19.18 kmpl<br>
        Category C (2001cc and above) – Mohammed Hilal and Ahmed Hilal with 
        19.09 kmpl<br>
        Category D (4WD) – Shailesh and Bhawna with 15.04 kmpl<br>
