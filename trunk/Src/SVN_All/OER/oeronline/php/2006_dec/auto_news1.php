		<font face="Verdana" style="font-size: 13pt">
              <b>Fuel Challengers</b></font></p>
              <p align="justify" style="margin: 5">
              <img border="0" src="autonews7.jpg" align="left" hspace="5" vspace="2" width="150" height="92"><font face="Verdana" size="1">John 
              and Helen Taylor, the couple from Australia, have been obsessed 
              with breaking records. Between them, they hold 34 notable 
              achievements for driving economically. Early this year, they set a 
              new Guinness World Record for travelling round the world in 78 
              days, covering 25 countries and 28,970 km on just 1,303 litres of 
              Shell fuel, averaging 4.5 litres per 100 km. OER had a chat with 
              them on the sidelines of the Shell Better Mileage Challenge held 
              in Oman recently:<br>
              <b><br>
              What brings you to Oman?</b><br>
              Helen: We are here to educate the public about driving efficiently 
              to save fuel and maximize the mileage on the invitation from Shell 
              Oman. We have given fuel saving tips to the public through our 
              interaction at various public forums organised by Shell. We 
              encouraged the enthusiastic participants at the Shell Better 
              Mileage Challenge on November 23.<br>
              <b><br>
              How was your record breaking journey from Muscat to Salalah?</b><br>
              John: It was wonderful. As part of the Shell One Tank Challenge, 
              we drove from Muscat to Salalah on November 20. We covered a 
              distance of 1,029 km in Volkswagen Golf 1.6L manual transmission, 
              provided by Wattayah Motors. We managed a mileage of 21.44 km per 
              litre which is 55 per cent higher than the official manufacturer’s 
              figures. <br>
              <b><br>
              How you manage your kids as you are busy constantly in challenging 
              journeys?</b><br>
              Helen: They have also inherited our passion for fuel economy. We 
              have three children — Kate 24, Lyndal 21, and Clint, 18, and each 
              child has personally experienced a record breaking drive 
              first-hand in Europe, Indonesia and Australia. <br>
              <b><br>
              How you developed a passion for fuel economy?</b><br>
              Helen: I was born in a family where money was scarce. We didn’t 
              own a vehicle. The extent of our travels was an annual visit to an 
              aunt’s house once a year by taxi. This lack of opportunity made me 
              determined to drive. I studied and worked hard to get my own car. 
              It all started in 1991 when two of my friends approached me as 
              they were recruiting four drivers to take part in a Guinness World 
              Record around Britain fuel-economy attempt. Without hesitation, I 
              joined them and we set a new world record. I’m still going on…<br>
              <b><br>
              What about you John?</b><br>
              John: I like doing different things. My first record-breaking 
              endeavor was in 1979 when I undertook a 3,000 mile walk around 
              Europe to raise funds for a special needs project. I have 
              undertaken the John O’Groats to Lands End (an endurance race 
              through England, Wales and Scotland) a record 18 times, in the 
              form of walking, running, cycling and driving. I also ran a 3,000 
              mile relay race across the US. We run ‘Taylor Made’ coaching 
              schools in Australia and Singapore. In my spare time, I write for 
              a motoring magazine in Australia. For us, 10 hours a day in a car 
              is a delight. After years of immersion in the daily demands of 
              raising a family, we’ve finally got the chance to enjoy some 
              (record breaking) quality time together. We believe that if auto 
              drivers start following our tips it could lead to significant 
              energy saving in the world.</font></td>
