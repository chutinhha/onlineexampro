<font face="Verdana" size="1"><b>Renaissance launches two new vessels</b><br>
<img border="0" src="business_briefs3.jpg" align="right" hspace="5" vspace="5" width="150" height="152">Renaissance Services Azerbaijan subsidiary BUE Caspian has launched two new 
vessels to its offshore vessel fleet. With an investment of over US$60 million, 
the two new purpose built vessels have arrived and will begin operations in the 
Caspian region shortly. The Norwegian built Platform Supply Vessel (PSV) Citadel 
has joined the BUE fleet to commence operations in the South Caspian region. The 
Citadel is a 72-meter, DP2, multi purpose Caspian designed vessel with on board 
facilities for 70 personnel as well as moon pool diving facilities for sub sea 
work. Earlier, BUE Caspian also launched the state-of-the-art 60 meter Emergency 
Response and Rescue Vessel (ERRV) Baki in Singapore. The Baki will be on a 
10-year charter for BP Azerbaijan.<br>
<br>
The Baki is the first vessel to be built specifically to enhance the safety of 
personnel and assets offshore in the Caspian Sea. With two Daughter Craft 
capable of speeds of 30kts, the Baki can offer emergency cover in multiple 
locations at one time. “She is regarded as one of the most technically advanced 
vessels in her class and can safely facilitate the recovery of 92 personnel,” 
according to BUE Caspian General Manager, Roy Donaldson. “The launch of these 
two new vessels is a significant milestone for Renaissance as it further 
positions itself as the premier offshore support vessel operator in the region” 
said Renaissance Chairman, Samir J. Fancy. </font>

