<font face="Verdana" size="1"><b>Siemens launches regional company </b><br>
Siemens AG launched Siemens LLC, its new regional company for Oman formed as a 
joint venture with Waleed Associates. “We will be able to further develop and 
dedicate our experience for them, jointly creating innovative solutions that 
will add a lasting value to our customers’ businesses,” Ralf Fueller, CEO of 
Siemens LLC Oman said. “Siemens has been active in Oman for over 30 years and 
has had a continuous physical presence here since the early 1980s.” Throughout 
the last three decades, Siemens has realised several regional ‘firsts’, such as 
Oman’s colour TV station. Siemens has been particularly instrumental in the 
rapid development of Oman’s telecommunications networks, including the region’s 
first EWSD (Siemens Digital Switching System) project in 1983 and the launch of 
the first totally Siemens-built GSM network in 1996.<br>
