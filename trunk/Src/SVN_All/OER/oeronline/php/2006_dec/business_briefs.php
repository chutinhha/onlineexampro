<b><font face="Verdana" size="1">Omantel Net Profit up by 17.6%</font></b><font face="Verdana" size="1"><br>
Oman Telecommunications Company (Omantel) recorded a Net Profit after Tax of 
RO60.947 million (unaudited) during the 9-months period ending Sept. 2006 
compared to the Net Profit after Tax of RO51.8 million in the corresponding 
period of 2005 — a growth of 17.6 per cent. The company emphasised that the main 
reason for the increase in profit is primarily due to the impressive revenue 
growth achieved during the period. The total group revenue as of September 2006 
rose by 21.3 per cent to RO239.6 million compared to RO197.5 million for the 
corresponding period of the previous year. The gross revenue from mobile 
operations as of September 2006 is RO170 million.</font><p align="right" style="margin-left: 10px; margin-right: 10px">
