<font face="Verdana" size="1"><b><br>
L&amp;T-Zubair JV inks pact with Sohar Port </b><br>
<img border="0" src="business_briefs.jpg" align="left" hspace="5" vspace="5" width="180" height="137">Oman-based L&amp;T Modular Fabrication Yard LLC establishing the country’s first 
modular fabrication facility at Sohar, North Oman signed an SUA (sub-usufruct 
agreement) with Sohar Industrial Port Company. L&amp;T MFY is a joint venture of 
Larsen &amp; Toubro Limited (L&amp;T) and The Zubair Corporation. The new modular 
fabrication facility is the first of its kind in Oman, and would significantly 
augment the country’s industrial base by manufacturing latest-generation 
products for the offshore oil &amp; gas sector. Fabrication of a range of complex 
plants including Integrated Decks from 10,000 to 20,000 tonnes, Jack-up Rigs and 
FPSOs (Floating Production Storage &amp; Offloading vessels) will introduce a new 
dimension to Oman’s offerings and leverage its geographical position as ‘the 
Gateway to the Gulf’. In addition to the GCC countries, the products “made in 
Sohar” will find markets in Africa and South Asia. The yard is spread over an 
area of 400,000 sq.m. and will have state-of-the-art facilities for heavy 
structural fabrication, sophisticated equipment &amp; systems integration and 
testing, and load-out of ultra-large modules. The water-front captive jetties 
will be able to accommodate ocean-going vessels of 10-12 m draft. The plant will 
be a global centre of manufacturing excellence in its field and, as planned at 
present, will produce about 50,000 tonnes of finished goods per year.</font><p align="right" style="margin-left: 10px; margin-right: 10px">
