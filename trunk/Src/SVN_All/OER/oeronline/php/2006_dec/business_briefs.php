<font face="Verdana" size="1"><b>QUICK LOOK<font color="#800000"><br>
Moody’s upgrades ratings </font></b><br>
Moody’s Investors Service has upgraded Oman’s foreign and local currency country 
ceiling for bonds and bank deposits to A3/P-2, with a positive outlook, from 
Baa1/P-2.<br>
<br>
Moody’s has upgraded the long-term foreign currency deposit ratings of 
BankMuscat (BM) to A3 with a positive outlook, as well as its foreign currency 
senior unsecured debt rating to A3 with a positive outlook, from Baa1. At the 
same time, Moody’s has also upgraded the local currency senior debt ratings of 
BM to A3 with a positive outlook, from Baa1. <br>
<br>
Moody’s has also upgraded the long-and short-term foreign currency deposit 
ratings of Alliance Housing Bank (AHB) to Baa1/P-2 with a positive outlook, from 
Baa2/P-3. In addition, AHB’s long- and short-term local currency deposit ratings 
