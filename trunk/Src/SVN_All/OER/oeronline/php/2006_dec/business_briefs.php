International Co-operation (JBIC) to finance part of the second phase of Sohar 
Industrial Port.<br>
<br>
<font color="#800000"><b>Oman to use Dubai exchange to price its crude</b></font><br>
Oman will abandon its retroactive oil export price mechanism and use the new 
Dubai Mercantile Exchange (DME) crude futures contract. Oman is one half of the 
benchmark for over 12 million barrels per day of Middle East oil exports to 
Asia, alongside Dubai crude.<br>
<br>
Oman takes 30pc equity in DME. The Sultanate’s Ministry of Finance reached an 
agreement with DME to acquire a 30 per cent equity stake in the exchange.<br>
