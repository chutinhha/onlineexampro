<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><font face="Verdana" size="1">
<b>Back on Track</b><br>
July 24 – October 30, 2006: The attractive valuations along with continued 
positive earnings growth by companies on the MSM saw investors (both local and 
GCC) flocking back to the market. The revamp of the MSM index in July 2006, 
which saw companies like Oman Cables and Al Hassan Engineering being included in 
the industry sector index and Oman Oil being included in the services sector 
index, not only helped the market recover but enabled it to touch a new peak, 
just short of the 5,800 level on October 30.<br>
<br>
The period also saw an improvement in the operational efficiency of the market 
in volume terms, resulting from share splits by many frontline companies, which 
earlier had little floating stock. These included Bank Dhofar, Al Anwar 
Ceramics, Majan Glass, Oman Oil Marketing and Majan College, all of which split 
the nominal value of their shares to RO 0.100. Another change brought in by the 
MSM authorities was the change in the method of calculating the closing price of 
securities. Starting from August 29, closing prices of listed securities on the 
MSM are based on the average price, with changes in closing price only possible 
if trading takes place in a minimum 500 shares on the regular market.</font></p>

