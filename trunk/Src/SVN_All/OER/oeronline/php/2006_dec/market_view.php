<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><b><br>
</b><font face="Verdana" style="font-size: 13pt"><b>Best Performer in GCC<br>
</b></font><font face="Verdana" size="1"><b><br>
Muscat Securities Market (MSM) riding on the excellent performance across sector 
indices is now poised to cross the 6,000-mark in 2007</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he 
year 2006 has been a roller coaster ride for the Muscat Securities Market unlike 
the previous years that were a one-way climb. The market has oscillated between 
the trench of 4,660 points and a peak of 5,800 points, a percentage variation of 
24 per cent. What has been heartening to note is that overall performance of MSM 
vis-à-vis other GCC markets. The Dubai Exchange declined by a drastic 58 per 
cent between December 2005 and October 2006 whereas the Bahrain Exchange 
recorded a marginal gain of 1.76 per cent. In the same period, MSM has given 
positive returns of 18.97 per cent. Despite this being the best performance in 
the GCC market, MSM’s show pales in comparison to the 33.8 per cent returns it 
gave in the period 2001-2005. However, then, the sentiments were more 
rationalised and valuations which were stretched came in line with the 
historical past. As usual, support came from the annualised earnings growth of 
companies with a YTD growth of 11 per cent for the year ending June 2006. The 
positive performance was on the back of continued government expenditure on 
large infrastructure and industrial projects.<br>
<b><br>
MSM Gains Momentum</b><br>
January 1 – February 6, 2006: After showing a strong performance in 2005, the 
MSM components continued to perform remarkably well and crossed the 
psychological barrier of 5,000 in January and early February. The growth was 
evident with the MSM Index gaining 9.70 per cent to close at 5,406.95 points on 
February 6. All sectoral indices ended in positive territory, with the highest 
gain of 11.65 per cent witnessed by the banking/investment sector index. The 
main reason for the positive performance was the expected dividend yields for FY 
2005.<br>
<b><br>
The Downtrend </b><br>
February 6 – July 24, 2006: MSM, which began 2006 on a strong note, started 
witnessing heavy selling pressure between March and July, with the index 
shedding nearly 13.6 per cent to end at 4,669.45 points on July 24. The decline 
was marked by overall negative sentiments and the slump in the GCC markets. 
However, given the fact that market valuations looked attractive, many stocks 
represented a good buy and provided for stock accumulation in the later stages.</font></p>

