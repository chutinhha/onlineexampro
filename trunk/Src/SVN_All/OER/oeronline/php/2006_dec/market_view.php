<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><font face="Verdana" size="1">
<b>Anticipated Correction </b><br>
October 30 – November 16, 2006: Subsequent to the rally, in early November, the 
MSM witnessed an anticipated correction, specially because valuations were 
stretched. The general index thus declined by 5.88 per cent. Selling pressure 
was seen across the board and among the sector indices, with the 
banking/investment sector index registering a decline of 7.53 per cent. However, 
with the stocks listed in the table playing an active part in the MSM index, 
Fincorp believe that the index can end up gaining 10 per cent over current 
levels, given the expected performance for 2007 and the likely discounting base 
for the respective companies.</font></p>

