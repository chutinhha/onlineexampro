<img border="0" src="pastime.jpg" align="left" hspace="5" vspace="5" width="200" height="199">W</b></font><font face="Verdana" size="1">hen 
the going gets tough, the tough take off in a spirit of adventure. For most 
people, there is no better stress reliever than the outdoors: the open skies, 
glittering stars and the welcoming sounds of silence.<br>
<br>
Kim Jepsen, general manager, Oasis Lifestyle LLC, is just back from a relaxing 
weekend at Wahiba Sands, Sharqiyah. His stress-busting menu? Dune bashing, a 
glorious sunset and lots of silence. “I have to do it again, the serenity out 
there is totally rejuvenating,” he says, as we sip cappuccino on a bustling 
Sunday afternoon at Second Cup Café, Qurum.<br>
<br>
Work, he emphasises, is priority No. 1. “But it becomes essential to try and 
find ways to unwind on weekends, especially after one puts in over 10 hours of 
work on the weekdays.” Its an outdoor streak or impulse, if one can call it 
that, which has made Kim visit Nizwa nine times, apart from crossing a few wadis 
too. And, all this only within 10 months of his arrival in Oman. In Nizwa, his 
interest in architectural structures and building designs is rejuvenated, as he 
revels in the timeless magnificence of the age-old forts, the lively souqs and 
the quaintness of the old towns. <br>
<br>
On weekdays, however, he comes home to the pots and pans and the smells of his 
kitchen where he whips up a cordon bleu meal (complete with all trappings) for 
himself or, on occasions, for his friends. “Like most Danes, I too am passionate 
about cooking. It is very relaxing to be able to focus on something so creative. 
It is also therapeutic in a way.” “The response to my cooking has been positive 
so far,” he adds, with a chuckle, disclosing that one of his business suppliers 
has asked him to help out with a cookbook.<br>
A foodie to the core, he admits to having tried everything from ‘deep fried 
grasshoppers’ to ‘mosquito eggs’ to ‘raw whale fat’ — each a delicacy in the 
countries he has travelled to. Hearing this, I wince behind my cappuccino, 
wondering if he has any surprise for me. Luckily, Kim declares his fondness for 
Omani and Indian food. <br>
<br>
Relieved, I hastily turn the conversation away from food. “I like reading, 
especially biographies and fiction. I also draw but a bit. I have also tried to 
pick up golf.” For Kim, taking time out generally means lounging at home, quite 
surprising for one who is single and mobile. “I come from Denmark, which is a 
lot like Oman in the sense that once you finish your work, you go home and 
relax,” he says.<br>
<br>
Calling it a day, Kim offers some practical thoughts on stress and how to beat 
it. “Stress is one of those things that people in important positions have to 
cope with. But you should never get to a point when everything is your life is 
stressed out. You need to ‘reload your batteries’ and try and de-stress 
yourself.”<br>
<br>
Qualifying, he adds that there is certain stress that is part of one’s life. “If 
I had nothing to do, I would get very stressed. I believe a little stress keeps 
you on the edge but no stress is not good at all.”</font></p>

