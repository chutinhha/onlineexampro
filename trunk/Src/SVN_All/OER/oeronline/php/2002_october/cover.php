                  <td width="100%"><img border="0" src="../../images/2002_october/SOHAR-port.jpg" align="left" hspace="5" vspace="5" width="225" height="150"><font face="Verdana" size="4" color="#FF0000"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
                    </font></font>
                    <font face="Verdana" size="4" color="#FF0000">Sohar ahoy!&nbsp;</font><p><font face="Verdana" size="1"><b>The port of Sohar is getting set to herald a new era for the Sultanate of Oman. But an attractive incentive package needs to be developed to ensure a smooth sailing for the port, considering the tough competition in the region. An OER report</b></font></p>
                    <p><font face="Verdana" size="2">The upcoming Sohar Industrial Port is expected to start generating revenue from the year 2005, and spur economic development on an unprecedented scale. With an aim to supplement the diversification of the country's economy, the Port, along with the upcoming Sohar Industrial Estate, is planned to achieve the Sultanate's goal of increasing the industrial sector's contribution to the GDP from the current four per cent to 15 per cent by 2020. The signing of a concession agreement recently with Rotterdam Municipal Port Management (RMPM) reflects the Omani government's commitment towards development of infrastructure.<br>
                    The 25-year agreement provides for the incorporation of Sohar Industrial Port Company (SIPC) which will undertake the development and management of the Industrial Estate. The government and RMPM will invest equal capital in SIPC, which will be the chief administering authority of both the port as well as the industrial area. RMPM shall transfer its knowhow to the government of Oman in developing and managing a multi-user port and industrial complex, and train the local workforce.&nbsp;<br>
                    HE Malik bin Sulaiman Al Maamari, Oman's dynamic Minister of Transport and Telecommunications who has played a vital role in keeping the Port project on track, had earlier said in an interview, "The government has recognised the need to protect its investments and offer quality service to the SIP customers. This was one of the main reasons for teaming up with the port of Rotterdam in Holland, the largest industrial and commercial port in the world."<br>
                    Planned to be built in three phases, work on Sohar Port started in late-1999. The Phase-I package involves Daewoo Corporation, which has completed the construction of a breakwater (costing RO25 million), and Hyundai Engineering &amp; Construction, which has executed the dredging and reclamation (costing RO9.8 million) at the port. Turkey's STFA Construction is building an 850-metre long quay for RO12.84 million. Its contract also includes the construction of two liquid berths, mooring facilities and paving.<br>
                    Commenting on the port development, Khalid Mirza, Director, SIP, says, "We will have two liquid berths dedicated to Oman Oil Company (OOC) and SRC for a period of 20 years. During Phase-II, we will build a 260-metre quay for the upcoming fertiliser plant, which will again be leased out from day one of its completion. The process of building the third liquid berth has also started, and plans are afoot to erect the fourth during Phase-II for the aluminium smelter project."<br>
                    As per the earlier plan, the Phase-I of the Sohar Port project was supposed to consist of two liquid cargo berths, two bulk berths for the aluminium smelter, two cargo berths and a container berth. An additional berth shall be built for government use. The harbour will have a depth of 17 metres which will be deep enough to accommodate the fifth generation container vessels which are coming into service.&nbsp;<br>
                    <br>
                    <b>The bottlenecks</b><br>
                    <img border="0" src="../../images/2002_october/sohar.jpg" align="left" hspace="5" vspace="5" width="200" height="150">At a time when things look positive for SIP, a host of factors seem to have been delaying the schedule of the port. Phase-I was to have been completed by late 2001, but the construction of two more berths has pushed the deadline to August 2003. In fact, many in the shipping industry have concerns about the port once it commences operations between June and August 2003.<br>
                    "One needs to appoint a port operator, who will take a minimum of nine months to bring in a crane, since it will have to be made-to-order," says an industry source. He further states that the government so far has neither prepared a tender document nor called upon a third party to appoint such an operator. This process might delay the commencement of operations at the port itself.<br>
                    In June 2002, the Ministry of Transport &amp; Telecommunications (MTT) had extended the deadline for the submission of completed bids for the Phase-II development of Sohar Port. Seven contractors bought the tender documents. Bidders who put in their tender offers and expected to hear news of the awards in June may have to wait until at least September for the results.<br>
                    Moreover, slow pace of work and bureaucratic hurdles seem to have delayed the Port project from going onstream. "We will not start generating profits before 2005, because there won't be many industries set up in Sohar Industrial Estate, and our scale of operations will be low," explains Mirza.<br>
                    His analysis is based on the simple fact that there has not been enough response towards the industrial estate from the private sector. Market reports suggest that except for the Bahwans there is no private investor in the Sohar industrial area as yet. The Bahwans are developing Sohar International Urea and Chemical Industries, a fertiliser plant, at an estimated cost of US$534 million, with a capacity to produce 2,000 tonnes per day (tpd) of ammonia and 3,500 tpd of granular urea. The other much-talked-about projects - refinery, polypropylene plant, methanol plant and the aluminium smelter - are all government-related, and may experience a delay in their implementation owing to their sheer size.<br>
                    Mirza informs that the government will have to start repaying its loan from October 22 this year. The Sultanate has invested RO96 million in the development of Sohar Port so far, for which it has availed a substantial loan from Japan Export and Import Bank in March 1999. There is a possibility of the loan period getting extended. However, the commendable aspect is that the project has not exceeded its budget till now.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><b>Challenges</b><br>
                    <img border="0" src="../../images/2002_october/Sohar--13.jpg" align="right" hspace="5" vspace="5" width="200" height="138">Competition from the neighbouring countries, especially from the United Arab Emirates (UAE), with its several ports, has posed a question on Sohar Port's future. The UAE's ports - Fujairah, Jebel Ali and Rashid - are considered the busiest in the region. Sohar Port, therefore, has to tackle the competition and create a feasible business model before it can become profitable.<br>
                    "The tariffs at the ports in the GCC region range between three and 15 per cent. While Saudi Arabia and Oman have tariffs of 15 per cent each, Dubai has the lowest between three to five per cent," says an industry source. Moreover, most of the GCC ports are well-developed and have excellent infrastructure to cope with fifth and sixth generation container vessels.<br>
                    Further, things could get more difficult for Oman from January 2003, when the GCC states plan to introduce a unified tariff of five per cent.&nbsp;<br>
                    Oman will have to live up to a market that expects the country to offer a host of incentives, world-class facilities and business volumes, to attract shipments to Sohar.&nbsp;<br>
                    Mirza says, "Sohar port's profitability will not be affected by competition alone. It's because Sohar Port is primarily an industrial port, and the main cargo will either be imports or exports of the factories. And with four proposed mega industries in Sohar Industrial Estate, the port can look forward to a rather good internal rate of return (IRR) of 12 to 13 per cent assuming that industrial estate is filled with businesses.<br>
                    Like other ports in the GCC region, Sohar Port is planning to develop its own business model. Mirza feels that the port would do better in attracting investments and ships if it provides a tax holiday or a reduced tariff structure for at least the first five years. "This would make the route attractive for the ships, which normally generate business for the local areas," says Mirza.<br>
                    During the Gulf War, the anchorage facility at the port Mina Qaboos used to have 80 to 83 ships coming in daily, thus fetching RO300 for the port per day (300bz per tonne) from each ship. But the government raised the charge to 500bz per tonne, while Fujairah used the opportunity to open its port for free anchoring. Therefore, what was earlier a small village (Fujairah), has developed into a busy city today.<br>
                    The refuelling business is yet another factor that many experts feel will elude Sohar. Most of the ports in the GCC region have advanced refuelling facilities, whereas Sohar Port is not designed to have that this kind of infrastructure. Shipping experts in Oman feel that it will be difficult for a ship to halt at Sohar for refuelling, and it will prove expensive as the location is a little moreoff-the-beaten-track for most cargo ships. They argue much more investment to fuel more interest in Sohar.<br>
                    Charges for land, gas and electricity; taxes for importing raw materials and exporting finished goods; rates for insurance, etc., are expected to be released soon. Mirza explains, "We are giving final touches to the guidelines. With any one of this information missing, nobody would come to set up factories here. This is one of the factors delaying the project." He mentioned that after a three year delay, the water intake project for the industrial area was sent to the SIPC for finalisation on September 11, 2002. "Though the project is three years old, we have to start on it afresh, which means a further delay of few more months," says Mirza. But will the Sohar Free Trade Zone (SFTZ), a part of the project, be able to generate business? "It will," says Mirza, "provided the government puts the right policies in place in terms of foreign investments, incentives like cheap fuel, a duty structure that is competitive with the GCC ports, and minimal levying of taxes."<br>
                    The shipping companies in Oman doubt if the SFTZ will ever happen. The free trade zone in Salalah ran into problems recently when the US construction firm Hillwood Development pulled out of a joint venture with the Omani government. The government was expected to have signed an agreement earlier this year to set up the free trade zone in Salalah. Sources revealed that Hillwood was supposed to have held 40 per cent of the stake, and was expected to have started constructing the Phase-I of the zone, costing US$90 million, this year.<br>
                    Notwithstanding, Sohar's port, industrial estate and future free trade zone will no doubt prove beneficial for the economy. As Mirza says, "It will help in creating new jobs that were not possible earlier. The construction of the port and movement of traffic will lead to new businesses like workshops, container repair shops, ship fuelling storage areas, painting shops and so on."&nbsp;</font></p>
