            IS THIS BOOM FOR REAL</font></font><b><font face="Verdana" size="1"><br>
            <br>
            Real estate is a very attractive investment asset class in the GCC 
            states. Judging by the spate of developments in the past few years, 
            the sector is experiencing a boom. Is the Gulf market merely a real 
            estate bubble nurtured by an artificial market, waiting to burst? 
            OER finds out </font></b></p>
            <p align="justify"><font face="Times New Roman" size="4">
            <img border="0" src="im2.jpg" align="left" hspace="8" vspace="5" width="250" height="172">O</font><font face="Verdana" size="1">ver 
            the past few years, investment inflows into the GCC real estate 
            market from investment giants, international funds, domestic 
            institutions and private buyers targeting property has grown to 
            record levels. This comes as no surprise given the world wide drop 
            in interest rates and the perceived risk of investing in other 
            sectors. Not only is property delivering a stable high level of 
            income, it has also delivered better capital growth and a lower 
            level of volatility. Although the number of real estate transactions 
            is expected to increase in most of the world markets, flat or 
            falling rents will be accompanied by declines in capital values as 
            investors demand higher income yields. Economists forecast recovery 
            in GDP growth in all major markets for the year 2004. The US is 
            expected to lead the recovery with a strong growth in GDP and real 
            estate values.
            </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Rented 
            Accommodation &amp; Related Costs: A Realty Check</b><br>
            </font>
            <img border="0" src="im3.jpg" width="500" height="117" vspace="4"><font face="Verdana" size="1"><br>
            </font><font face="Arial" size="1">Figures in US$; Source: Economist Intelligence Unit</font><font face="Verdana" size="1"><br>
            <br>
            The real estate markets of GCC countries were not significantly 
            affected by the conflict in Iraq or the world economic slowdown. On 
            the contrary, statistics show increased prices as well as increased 
            demand and purchase activities across the six member nations. The 
            phenomenon can be attributed to increased liquidity, political 
            stability and the repatriation of Arab funds from the US following 
            the 9/11 attacks. These funds have been invested in domestic real 
            estate and equity markets, further boosting the economies of the 
            region. Another significant driving force has been the grant of 
            rights to GCC citizens for the purchase of real estate properties 
            within the block, as the GCC ultimately strives to achieve a united 
            economic block. There are massive real estate projects coming up 
            across the region.<br>
            <br>
            <b>Real growth </b><br>
            The real estate sector in the GCC is going through a period of 
            unprecedented growth owing to various factors such as high liquidity 
            levels resulting from firm oil prices, low interest rates, easy 
            availability of finance and renewed interest in domestic investments 
            by local investors on account of depressed global markets. Many 
            investors have turned to the real estate sector, which is expected 
            to provide higher returns and is also perceived as a safe long-term 
            investment. <br>
            <br>
            <img border="0" src="im4.jpg" align="right" hspace="6" vspace="6" width="150" height="266">Land prices and rental rates improved significantly in 2002 across 
            segments due to the various factors. In the residential segment, 
            land prices, which were relatively steady for several years, grew 
            moderately in 2002 throughout the region. Rentals in this segment 
            also increased in 2002 after several years of decline. With the 
            local population growing rapidly, the demand for residential housing 
            is expected to increase. This coupled with the concentrated holdings 
            of residential land is expected to result in prices of residential 
            land appreciating further over the medium term. <br>
            <br>
            In the commercial segment, land prices have grown significantly in 
            the urban areas while in other areas, it has remained relatively 
            stagnant. Commercial rentals have, however, increased in most areas.
            The real estate sector is expected to exhibit a higher-than-average 
            growth rate in coming years on the heels of a recovering regional 
            economy that appears to be gaining momentum, which has encouraged 
            both locals and expatriates (in countries where it is possible) to 
            pool their investments in the real estate sector to hedge their 
            wealth against inflation. Moreover, regional governments rely on 
            this sector to further activate economic activity and growth, since 
            numerous industries are directly related to the real estate sector 
            activity. <br>
            <br>
            <b>Real investment </b><br>
            With a wide variety of investment instruments appearing on the 
            market, is there still a future for traditional real estate 
            investment? According to official figures released by the Arab 
            Monetary Fund (AMF) in May last year, capital outflows from the GCC 
            during the past 10 years amount to approximately $240 billion 
            (RO92.31 billion). More than 60 per cent of these outflows emanated 
            from Saudi Arabia, and property markets in the US and Europe were 
            the prime beneficiaries of Arab capital seeking overseas 
            opportunities. Although recent figures show a flight of capital away 
            from the US since 9/11, some markets, such as the UK, are still 
            proving extremely popular with Middle Eastern investors. So why is 
            this, and is this trend likely to continue? <br>
            <br>
            Like gold, property or real estate have the gleam of a 'safe' 
            investment in times of turmoil, without the tarnish that other asset 
            classes may bring. Three years of poor equity market returns served 
            to remind investors of the dangers of being overweight in equities, 
            plus changes in monetary policy in European markets have lowered the 
            cost of debt capital leading to a renewed interest in the property 
            sector. Financial institutions are also keen to increase their 
            exposure to a sector that has a weak correlation with the highs and 
            lows of stocks and bonds. <br>
            <br>
            Real estate is fast becoming a very attractive investment asset 
            class in the GCC states. Judging by the spate of real estate 
            developments, funds, and the emergence currently of housing finance 
            and securitisation, the sector is experiencing a boom that is making 
            many investors and consumers jittery. Is the Gulf market merely a 
            real estate bubble nurtured by an artificial market, waiting to 
            burst at the sign of any serious worsening of political instability 
            or a dramatic fall in the price of oil and its inevitable knock-on 
            effect on the other sectors of the economy? <br>
            <br>
            Most Gulf bankers agree that a correction in the real estate market 
            is due, but they differ on the size of the correction - 5 per cent; 
            10 per cent; or 20 percent. In Kuwait for instance, the market is 
            already on a downward trend. Margins, according to one banker, are 
            low - up to 2 per cent. The market is flush with liquidity as 
            Kuwaitis are repatriating more of their funds home. The municipality 
            has also increased the size of rentable areas of developments. Land 
            values have appreciated sharply from 20 Kuwaiti Dinars (KD) per sq 
            ft only a couple of years ago to over 52 KD per sq ft at present.
            <br>
            <br>
            Subsequently, yields from real estate developments, subject to the 
            area and type of development, have declined from 12 per cent to 6 
            per cent in general. The areas allocated for commercial or 
            residential real estate development is very limited because most of 
            the land belongs to the government and are oil production areas. <br>
            <br>
            The internal dynamics of the Gulf real estate market also lends 
            itself to a dichotomy, which at best can be perceived as both 
            reformist and confusing. Yes, foreign ownership of property is now 
            tolerated. But under such protectionist conditions that make a 
            mockery of any espoused conviction to a truly free market economy.
            <br>
            <br>
            <img border="0" src="im5.jpg" align="right" hspace="6" width="250" height="166">In Dubai, for instance, foreigners can buy freehold properties only 
            in certain designated so-called property free zone areas, which are 
            limited in numbers and are usually aimed at the higher end of the 
            market. Otherwise, they can rent or buy on short lease-holds. <br>
            <br>
            Bahrain, the UAE, Qatar, and Kuwait are in the throes of a classic 
            real estate boom - both in terms of prestigious commercial property 
            projects such as Bahrain Financial Harbour; the Amwaj Island Resort 
            in Bahrain; the Dubai International Financial Centre; Dubailand 
            Theme Park as part of the Arabian Legends Resort Complex; and so on.
            <br>
            <br>
            However, by far the biggest market is Saudi Arabia, especially the 
            environs of Riyadh, which is seeing an unprecedented number of real 
            estate developments. The Saudi capital, according to various 
            studies, is earmarked for significant economic development over the 
            next decade or so. The size of the real estate sector in the 
            Kingdom, according to one Saudi asset manager, is in excess of SR1 
            trillion and is second only to the oil and gas sector. <br>
            <br>
            <b>Beyond GCC </b><br>
            However, not all Gulf investors are keen to put their eggs in one 
            basket. As such real estate funds, promoted by Gulf banks, are also 
            targeting deal flows and developments such as multifamily and 
            assisted living in the US, UK and Europe. First Islamic Investment 
            Bank in Bahrain, for instance, has a US real estate portfolio of 
            over $1.2 billion. Gulf Finance House in Bahrain too has Gulf 
            Atlantic Real Estate Funds in the UK and in France, and is in the 
            process of closing its first Spanish real estate fund, the $50 
            million Al Andalus Fund. Dubai Islamic Bank similarly has just 
            launched a 179 million Euro Al-Islami French Property Fund. <br>
            <br>
            All the above funds have one feature in common. They are all Islamic 
            real estate funds. And the demand for such funds and products seems 
            insatiable. The Dallah Albaraka Group (DBG) recently closed 
            subscription to its SR140 million Al Shurook Real Estate Fund two 
            weeks after the launch because demand was so high. DBG in the last 
            few years has launched and closed several such funds including 
            Durrat Al-Aroos; Al-Madinah Al-Munawarrah; Al-Riyadh; and Al-Omran.
            <br>
            <br>
            June's Islamic Real Estate Finance (IREF 2004) conference in 
            London organised by Islamic Conference Group, stressed the dramatic 
            growth of the market and the challenges relating to new markets; 
            product innovation; entry and exit strategies; outperformance; 
            benchmarking; indexing; valuation; retail housing finance; Sukuk 
            (bonds and certificates) and securitisation; and Shariah compliance.
            <br>
            <br>
            The City of London was pitching strongly to get a bigger slice of 
            this market. The Lord Mayor, Alderman Robert Finch, who inaugurated 
            the conference, stressed that ''real estate is a cornerstone of many 
            Islamic asset management strategies. The development of more liquid 
            instruments to deepen this market is a new and crucial initiative.''
            <br>
            <br>
            Others berate the Islamic sector for its moral ambivalence in the 
            real estate sector. They point to the fact that, save perhaps 
            Malaysia and Kuwait, most of the Islamic real estate developments 
            are concentrated at the high net worth end of the market. They rue 
            the lack of key worker and low-cost housing developments. They point 
            to the pockets of poverty and lack of proper housing in all the Gulf 
            states. This at a time of supposedly huge liquidity in the market.
            <br>
            <br>
            Perhaps the most revealing thing that came out of the conference is 
            the huge gap in the enabling factors such as legal infrastructure, 
            tax issues, and the use of special purpose vehicles under certain 
            jurisdictions. Issuing an asset-backed Sukuk (Islamic bond or 
            certificate) under Saudi law, for instance, is not feasible. As 
            Trevor Norman of Volaw Trust &amp; Corporate Services Limited in Jersey 
            explains, ''Sukuk are not recognised under Saudi corporate law as a 
            valid instrument to be issued by a Saudi registered company.'' <br>
            <br>
            By Saudi law, the owner of the asset (either real estate, vehicles, 
            plant etc.), has to be a Saudi national, hence the establishment of 
            a Saudi SPC (special purpose company), which in turn also had to be 
            owned by Saudi nationals. For the Jersey SPV (special purpose 
            vehicle) to retain economic control of the assets (as per the 
            requirement of the Islamic investors), a complex trustee and agency 
            solution had to be found which satisfied both Saudi law, Jersey law 
            and the requirements of both the issuer of the Sukuk and the 
            investors therein. <br>
            <br>
            In fact, this was the structure used for a recent Sukuk, Caravan 1 
            which involved Saudi private sector transport assets. The issues 
            raised equally apply in general to real estate assets in the 
            Kingdom. ''This transaction,&quot; added Trevor Norman, really brought 
            home the differences between the Civil Law and Common Law 
            jurisdictions, and the one problem we didn't solve was the 
            uncertainty over enforcement of Saudi laws.'' <br>
            <br>
            Islamic finance, stressed one banker, operates in an environment of 
            'negative consent' in some of the Gulf states, where it is tolerated 
            but not proactively supported through the regulatory, supervisory, 
            and legal infrastructure. This dichotomy is not only confined to the 
            Islamic finance sector, but also in the economy in general. <br>
            <br>
            The real estate industry has gone through a boom-and-bust cycle over 
            the last few decades. The following table suggests that the region's 
            real estate industry has enjoyed 10-year periods of growth 
            punctuated by shorter periods of recession for the past 25 years. We 
            are currently in a recovery period, which is expected to accelerate 
            in the coming years. <br>
            <br>
            <b>The Real Estate Economic Cycle </b> <br>
            <img border="0" src="im6.jpg" vspace="4" width="189" height="83"></font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Middle East interest </b><br>
            <b>
            <img border="0" src="im7.jpg" align="right" hspace="6" width="200" height="117"></b>The rise in popularity of property as an investment in bear markets 
            has not been lost on financial institutions in the Middle East. The 
            recent slew of real estate investment funds based on international 
            property in the US and Europe, and increasingly within the Middle 
            East, has kick-started an industry that previously catered largely 
            for direct property investment by affluent individuals and families.<br>
            <br>
            Direct investment through real estate funds can be an ideal building 
            block for enhancing and safeguarding the assets of an investor's 
            portfolio. They are particularly beneficial to investors who are 
            heavily concentrated in more traditional asset classes, and are, 
            therefore, a good way of diversifying a portfolio. <br>
            <br>
            The bear markets in recent years have seen an investor focus on the 
            preservation of capital. Pension funds in Europe, for example, 
            typically have between 5 and 10 per cent of their portfolio in 
            property, but because of low interest rates and volatility in equity 
            markets, these investors have reassessed their portfolios and have 
            given property a higher weighting in recent years. This sentiment is 
            now the same in the Middle East, where investors are looking for 
            diversification and a credible instrument to get into. <br>
            <br>
            <b>Real Estate Islamic FIs </b><br>
            Investment in the real estate market has been natural progression 
            for Islamic financial institutions (IFIs), and has provided a much 
            needed investment diversification tool. Indeed, IFIs have led the 
            charge in creating real estate funds and have shown a healthy degree 
            of innovation in their recent activities in the real estate market. 
            Islamic banks are increasingly involved in financing large real 
            estate transactions in order to stabilise returns on investment 
            deposits and to improve their balance sheets with relatively secure 
            assets. The past three years have witnessed a rapid increase in the 
            number of investment funds based on real estate transactions. <br>
            <br>
            These investment funds tap into a potentially huge market, with 
            pent-up demand for Shariah-compliant US and European property assets 
            conservatively estimated to be worth $30 billion (RO11.45 billion).
            <br>
            <br>
            The majority of the IFI real estate investment products are aimed at 
            high net worth individuals, and the funds are used to finance assets 
            for capital appreciation, or for the development or acquisition of 
            rent generating assets. These are typically done through special 
            bank deposits or private investment funds. Further development of 
            this market will provide the base for capital market instruments 
            that would enable the creation of viable local and international 
            Islamic money markets - for instance, the development of an Islamic 
            mortgage-backed securities (MBS) market. Such securitised real 
            estate transactions will help address long-standing difficulties 
            faced by IFIs. Securitised real-estate transactions will allow IFIs 
            to intermediate in long-term investments, create instruments that 
            can be rolled-over continuously into secondary money markets, and 
            resolve the difficult issue of foreign exchange hedging by creating 
            comparable financial obligations in different currencies. <br>
            <br>
            In the UAE, where it is difficult to miss the real estate boom with 
            billboards advertising the sale of villas on man-made islands, Dubai 
            Islamic Bank (DIB) has created a joint venture together with 
            Istithmar and Island Capital Group to launch commercial mortgage 
            backed securities. These securities will be listed and traded on 
            regional and international markets, including the US and Europe. The 
            joint venture, named Emirates National Securitisation and Finance 
            Corporation, will securitise pools of mortgages and issue them as 
            bonds to the tune of $1 billion. The mortgages will be kept in a 
            trust vehicle, and once the securitization is complete the funds 
            will be returned to the banks. The banks can then recycle the 
            capital, further improving liquidity. <br>
            <br>
            These mortgage-backed bonds are expected to boost the local real 
            estate market, in addition to attracting much-needed liquidity into 
            the nascent capital market. The development of this bond is also 
            expected to iron out snags in the development of the fledging 
            mortgage industry in the UAE. At present mortgages in Dubai are 
            offered only by a handful of local banks, although this is set to 
            change. HSBC, for example, entered the mortgage market for Nakheel 
            properties earlier this year, and will compete with the likes of 
            Amlak, the mortgage finance arm of Emaar Properties. <br>
            <br>
            Even the historically insular market of Saudi Arabia is now opening 
            its doors to foreign investment. Sheikh Khalid Al Gahtani, President 
            and CEO of Al Khobar Rikaz Development and Vice-Chairman of the 
            National Real Estate Committee of Saudi Arabia, stated at a real 
            estate conference that Saudi Arabia's real estate industry is 
            undergoing dramatic growth, with both local and foreign capital 
            competing for opportunities. He estimates that $6 billion (RO2.31 
            billion) was invested in the Kingdom's real estate in the last 
            quarter of last year. <br>
            <br>
            He also noted that the growth is pushing forward the regulation of 
            the industry, which he anticipates will fuel investment, providing 
            opportunities for the small to medium-sized investor. Regulations 
            allowing for the formation of housing associations, plus a framework 
            which will also cover investment laws for foreigners, will likely 
            further boost freehold investment in the Kingdom, and pave the way 
            for local real estate backed funds. <br>
            <br>
            Real estate research firm DTZ recorded almost euro 16 billion of 
            cross-border purchases in European real estate markets in the first 
            half of 2003, with the French market overtaking the UK market for 
            the first time as the favoured destination for cross-border 
            investment. Europe continues to attract the attention of real estate 
            funds in the Middle East, despite the opening of the real estate 
            markets in the Middle East. This looks set to continue, with 
            landlord-friendly lease structures, liquidity, transparency, and 
            relatively low transaction costs prevalent across Europe. <br>
            <br>
            European offices continue to be the preferred property type, 
            accounting for 44 per cent of all activity during the first half of 
            2003 according to DTZ, with central London and Paris markets still 
            dominating purchasing activity. Commenting on the enduring 
            desirability of the former, Naomi Heaton, CEO of London Central 
            Properties, says: &quot;Central London continues to be desirable because 
            of its continuing economic and political stability and because of 
            its importance as a financial centre. In addition, there is very 
            little new land development potential in the centre of London, which 
            means that those investors who choose to buy into some of the best 
            addresses in the world are buying into an increasingly scarce 
            resource. We view it rather like a diminishing 'slice of cake', with 
            decreasing availability of stock likely over the forthcoming years.&quot;
            <br>
            <br>
            The continued popularity of property as an asset class would 
            therefore seem set to continue unabated. However, it remains to be 
            seen if the tide of capital from the Middle East, traditionally from 
            private direct investors and increasingly through the establishment 
            of property funds by conventional and Islamic Middle East 
            institutions, will continue to plough into overseas markets given 
            the increasing availability and popularity of home property markets.</font></p>
