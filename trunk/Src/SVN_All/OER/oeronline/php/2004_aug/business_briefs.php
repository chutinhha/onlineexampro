<p align="justify"><font face="Verdana" size="1"><b>Oman rated the 18 freest economy
in the world</b> <br>
<br>
Oman has been ranked as the 18th freest economy in the
world by the CATO institute in the US in its annual
report, Economic Freedom of The World. The report carried
out a survey of 123 countries. CATO ranked Hong
Kong and Singapore in the first place whereas US stood in
third position.
<br>
<br>
This economic survey is based on five variables which
determine the economic freedom in a country. These five
categories include the size of government, which is determined
in part by spending taxes, the legal structure and
security of property rights, access to sound money that is
not weakened by high inflation rates, the freedom to
exchange goods and services with foreigners and the
degree to which business and credit and labour markets
are regulated by the government.</font></p>

