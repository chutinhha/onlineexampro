<p align="justify"><font face="Verdana" size="1"><b>Oman's GDP up</b> <br>
<br>
<img border="0" src="im40.jpg" align="right" hspace="5" width="150" height="136">The domestic macro-economic environment
of the Sultanate has
improved considerably in 2003 as
reflected in higher estimated
growth in GDP, modest decline in
prices, easy liquidity condition, surpluses
in the fiscal and current
account positions, higher foreign
exchange reserves and notable
increase in employment, in both
public and private sectors.
<br>
<br>
The Central Bank of Oman, in its
annual report for the year 2003, gave a comprehensive review of the domestic
economic developments in the recent period with particular reference to 2003. 
With
high oil prices, comfortable balance of payment, fiscal, liquidity and foreign
exchange reserves positions, the outlook for 2004 also looks positive, the 
report said.
<br>
<br>
Given the predominant role played by the oil sector in shaping the course of
the economy, despite the decline witnessed in oil production in recent years, 
the
economy remained vibrant owing to increase in average prices for Omani crude
from $24.29 in 2002 to $27.84 in 2003, the annual report said.<br>
<br>
While oil exports increased by 4.4 per cent in 2003, net oil revenues of the 
government
rose by 5.3 per cent, and value added by the petroleum sector at current
prices also moved up by 6.5 per cent. Stronger contribution of the oil sector 
helped
in lubricating the non-oil sectors, which was manifested in appreciable growth 
in
non-oil exports, non-oil fiscal revenues and value addition in the non-petroleum
sectors. Oman's gross domestic product (GDP) at current prices is estimated to
have increased by 6.3 per cent in 2003, as against 1.8 per cent in 2002.

