<p align="justify"><font face="Verdana" size="4" color="#FF0000">Markets take a 
breather</font><font face="Verdana" size="1"><br>
<br>
<b>Fincorp examines the latest stock movements on 
the Muscat Securities Market as the re-based MSM
Index stabilises after a period of hectic activity
</b><br>
<br>
<img border="0" src="im43.jpg" align="left" width="200" height="150">The MSM markets stabilised during
the period under review (June 20 to
July 22, 2004), forming a top at
around 3487 levels before pulling
down on profit-taking during the
July period. There was a momentary upswing
when the MSM Index was reconstituted during
the beginning of July; however, it was
short-lived, with profit-taking especially in the
industry and banking stocks resulting in a fall.
This resulted in the MSM Index closing at 3409
levels, a slight drop of 0.83 per cent on a
monthly comparison basis.
<br>
<br>
In spite of this, on a broad level, the overall
advances continued to lead the declines
with a ratio of 1.4, suggesting that bullish sentiments
continued to prevail in second rung
stocks as predicted in our previous column.
The average volumes during the period under
review remained healthy at more than
RO3 million.
<br>
<br>
However, the overall trend in the volumes
suggests that there has been a gradual slowdown
in the value traded with the latest fortnight
during the period under review trading
below the RO3-million mark. <br>
<br>
<b>Stalwarts lead the decline<br>
</b>The companies that were responsible for the
decline include star performers of the current
year - Oman Cement, Raysut Cement and
BankMuscat. Others included Al Khaleej
Polypropylene, Alliance Housing Bank, ONIC
along with Shell Oman Marketing and
BankDhofar. In fact, the downturn in the
Index would have been more accentuated had
it not been for the timely MSM reconstitution
as companies like Oman National Electric,
United Power and Muscat Gases further went
down during the period.
<br>
<br>
<b>Small cap stocks in the limelight</b> <br>
Small market cap-based companies like Oman
Ceramics have been the saviours of investors
during the period under review, largely on back
of speculation as well as news of restructuring
exercises. Oman Ceramics has decided to go in
for a capital restructuring programme which
includes reducing its equity capital by adjusting
it against accumulated losses. Other companies
in its league that flared up included Dhofar
Fisheries, Oman Filters and Sweets of Oman.
<br>
<br>
Other prominent companies under the regular markets that supported the indices included
flour companies like Salalah Flour Mills and
Oman Flour Mills along with Al Anwar
Ceramics, Oman Textiles and Dhofar
Cattlefeed.<br>
<br>
<b>IPOs coming in vogue</b><br>
Following the Dhofar University IPO issue in
June, another IPO coming for subscription in
the last week of July was that of Al Kamil
Power Company, which was set to open on
July 27, 2004, with an issue size of 3.369 million
shares at a par value of RO1 per share
plus a premium of RO0.690 and issue expenses
of RO0.020 per share.
<br>
<br>
The total issue size is slated at RO5.76 million.
The divestment shall constitute 35 per cent
of the company's paid-up capital of 9.625 million
shares, with the remaining 65 per cent continuing
to remain with the founder promoters.
The issue will close on August 25, 2004.
However, unlike earlier offerings, the current
offering is open only to Omani nationals and
entities incorporated in Oman. This could well
be followed by the larger issue of Al Barka
Power Project within a couple of months.
<br>
<br>
<b>Second quarter results
<br>
</b>The result season also began in earnest with
the announcement for NBO and Alliance
Housing Bank results. NBO continued to
show steady results while Alliance Housing
Bank continued to grow on its books leading
to a substantial jump in its overall profitability.
Other companies that have enhanced their
performance include ONIC and Oman
Chromite while Oman Ceramics, Modern
Poultry and Dhofar Fisheries reduced their
overall losses on a YOY basis for the six
months ending June 2004.
<br>
<br>
<b>Outlook: Markets expected to remain
choppy
</b><br>
The last few days of the period under review
has seen the markets tapping into stocks
which have not moved during the current
rally at the expense of fundamentally stable
stocks, but with overstretched valuations. We
believe that this theme would continue for a
few more weeks. Further, investors could also
closely follow stocks that have fundamentals
in place but with earning surprises from
expansion related or investment activity with
a long-term perspective. These include stocks
like Al Anwar Ceramics, Oman Chlorine and
National Aluminium et al. Hence, money
could rotate from these stocks from the frontline
stocks along with companies that have
been able to maintain their earnings in the past
like Topaz Energy &amp; Marine, Port Services,
Omani Packaging and Salalah Flour Mills. We
advise investors to remain vigilant in taking
any fresh positions as the markets look for a
new direction in the short term.

