<p align="justify"><font face="Verdana" size="4" color="#FF0000">Smooth operator
</font><font face="Verdana" size="1"><br>
<br>
<b>The Corolla is the world's most popular car. Vicky Kapur gets behind the 
wheel of the new Toyota Corolla 2005 and discovers just why</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im42.jpg" align="left" hspace="5" width="243" height="113">First, the facts. The Corolla is
Toyota's sedan and the
world's best-selling car, with
more than 27 million units
sold in over 142 countries
since its 1966-debut in Japan. With
Saud Bahwan Automotive representing
Toyota in Oman, the brand occupies
pride of place among the vehicles
seen on the Sultanate's roads. In fact,
Toyota enjoys a majority share of
Omani roads, with an estimated 40 per
cent-plus market share in the country.<br>
<br>
That, by far, is the largest market
share enjoyed by any manufacturer in
this part of the world. And, according
to the local dealers, it's all thanks to the
''bread-and-butter'' Corolla, which sells
like hot cakes in the Sultanate, where it
has been a conservative economy car
with a nice ride and balance and a car
that seems to have acquired a cool 'runforever'
reputation.
<br>
<br>
The current model of the car is the
ninth generation Corolla. It was given
a major change for 2003 to make it
more alluring to young drivers in the
face of greater competition from South
Korean economy cars with more
equipment and lower prices. The
Toyota Corolla is rated among the best
of the sedans: reliable and economical,
yet refined and comfortable. The 2005
Corolla line features a restyled front
fascia and redesigned tail lamps.
<br>
<br>
A Corolla equipped with the optional
leather interior almost feels like a
miniature luxury car. New safety
options for the 2005 Corolla sedan
models include side curtain airbags
with seat-mounted side airbags and a
vehicle stability control system (VSC),
the latter designed to eliminate skidding.
This is in addition to optional
anti-lock brakes with electronic brakeforce
distribution, which improve
steering control in an emergency stopping
situation and enhance braking
stability and effectiveness.
<br>
<br>
<b>Exteriors
</b><br>
The 2005 Corolla features a newly designed
front bumper, grille and headlamps.
At the rear are subtly revised tail
lamps. The Corolla looks like a smaller
Camry. Its ovoid headlights are similar
and its rakish yet curved shape is in
keeping with the newest Camry. Thick
A-pillars at the front and thick C-pillars
at the rear give the Corolla a solid look
while hefty bumpers add to its substantial
appearance.
<br>
<br>
Corolla comes standard with a long
wheel base (2600 mm) and wide 185/70
R14 tyres for better road grip. The topof-
the-line 1.8 GLi Spl Corolla (RO6,675)
that OER test drove, however, comes
packed with wider 195/60 R 15 tyres
and sporty and light-weight alloy
wheels for an even better road grip. <br>
<br>
At the rear, the Corolla has a high
trunk line in keeping with the car's
high belt line. The taillights match the
shape of the headlights nicely. Part of
the taillight module is actually mounted
in the trunk lid. This is fine except
that the design causes the trunk lid to
be narrower than it might be.
<br>
<br>
The doors have reasonably wide
openings, which makes getting in and
out easy. That's a benefit of the long
wheelbase, which also means that the
rear wheel wells do not protrude into
the rear door as much as they do on
some other cars. Another benefit of a
longer wheelbase is shorter overhangs
at the front and rear (the distance from
the wheels to the ends of the car). Short
overhangs improve the car's looks as
well as the handling. The 1.8 GLi Spl
Toyota Corolla receives a new front
spoiler and fog lamps, and smoked
headlamp masking.
<br>
<br>
<b>Interiors
</b><br>
The Corolla's interior doesn't feel cheap,
like it does in some other cars in its category.
There's plenty of front legroom
and headroom, and there's a decent
amount of room in the back for two
average-size adults although there are
seat belts for a third rear-seat passenger
as well.
<br>
<br>
Materials used in the seats appear
classy with nice fit and finish. The driver's
seat has a much more substantial
feel than in many cars in this price
range, which makes it more comfortable
for long journeys. Even the doors
shut with a pleasant thud thanks to
sound deadening felt mounted inside
the door panels.
<br>
<br>
Take a look inside a 1.8 GLi Spl Corolla with optional leather seating, and you 
could easily imagine you're looking inside a luxury car. The leather seats have 
a luxurious soft pleated finish while the centre console and door
panels have wood-like veneer trim.
Add to that a sporty leather-wrapped
steering wheel, and the enjoyment of
driving is definitely enhanced.
<br>
<br>
Cloth or leather, the seats are comfortable
and supportive. Big heating and
air-conditioning controls, simple, straightforward
radio controls, well-designed
cup holders, and plenty of cubbies for
storing stuff make for a convenient interior.
Getting in and out of this four-door
sedan is easy and the back seat is surprisingly
roomy and comfortable.<br>
<br>
The dashboard is nicely laid out
with exemplary ergonomics and a finish
comparable to that of more expensive
cars. Its top surface sweeps across
the width of the car in a slight S-curve
and wraps into the door trim. Each
side of the centre console seamlessly
blends up to the lower portion of the
dash. Four small vents are nicely inlaid
into the upper part of the dash.
<br>
<br>
The radio is located high up so it is
easy to reach while the three large
round knobs for the heating and air
conditioning system are located below.
The handle for the parking brake is
located on the left of the centre console
so there's room for a small storage cubbyhole
on the right. Storage spaces are
quite generous with small pockets in
each door and the glove box is large.
<br>
<br>
Three overlapping round gauges in
the instrument pod contain a large
speedometer, a smaller tachometer to the
left and a water temperature gauge and
fuel gauge mounted in a matching circle
to the right. They incorporate red lettering
on a plain white background and are
easy to read at night. There's also an outside
temperature readout on all models.
<br>
<br>
The rear seats split 60/40 (in the
1.8GLi and 1.8 GLi Spl trims) to allow
access from the trunk although the
opening is not that large, limiting passthrough
with large cargo. The trunk is
one of the biggest in this class. All in all,
the interior has a high level of refinement
beyond what one expects in a car
in this price-range.<br>
<br>
<b>Performance</b> <br>
The Toyota Corolla is smooth, convenient,
and easy to drive. Frankly speaking,
it's hard to find faults with this
car. The standard 145-horsepower
four-cylinder engine is quiet, smooth
and refined. It delivers brisk acceleration
performance with a broad power
band for nice, linear acceleration.
There is no difference in engine, transmission
or suspension between the
various trims.
<br>
<br>
The electronically controlled fourspeed
automatic transmission is nicely
matched with the engine. It includes a
shift logic system that helps hold a
gear longer than it might otherwise if it
senses the car is going up an incline or
the driver wants plenty of power while
passing. For those who enjoy doing
their own shifting, the five-speed manual
allows for more performance as the
engine's power can be better utilised. It
shifts easily via a relatively tall shifter.
Both the gearbox and clutch are smooth
and easy to operate.
<br>
<br>
The power steering is just right,
providing a nice feel without being too
stiff or sloppy. The suspension has
been tuned to give a smooth ride,
which is helped by a relatively long
wheelbase. Body roll (lean) is minimal
and the car feels secure and stable at all
speeds. The suspension is soft and does
a good job of soaking up bumps and
road vibration, giving the Corolla a
nice, smooth ride that we really liked.
<br>
<br>
<b>Safety
<br>
</b>The Corolla 2005 offers world-class
safety features as standard. These
include the 3-point emergency locking
retractor for the front seat belts with
additional auto-locking retractor for
those in the rear, ventilated disc brakes
(front) and disc brakes (rear) for better
braking efficiency, SRS airbag (driver)
to reduce upper body injury during a
collision (passenger airbags included
for the 1.8 GLi and 1.8 GLi Spl trims),
pre-tensioner on driver and passenger
seatbelts to assist occupant restraint
during a collision, force limiter on
driver seatbelt, shatter-proof green
laminated windshield glass, global
Outstanding Assessment Car Body to
protect occupants by minimising cabin
deformation during a collision and
Whiplash Injury Lessening concept in
front seats to minimise vertebral injury
during rear-end collision.
<br>
<br>
In addition to the above, the 1.8 XLi
PWR trim (RO5,775) gets power door
lock, window and outer mirror operations
and front bumper corner protectors
while the 1.8 GLi Spl comes
loaded with anti-lock brakes (ABS)
with electronic brake-force distribution
according to driving conditions
and vehicle load, moon roof (tilt and
slide type) and side-impact airbags.
<br>
<br>
<b>The finishing line
</b><br>
It isn't too much of a stretch to say the
Corolla is a miniature luxury car. The
Corolla boasts a high-quality, ergonomically
excellent interior with comfortable,
supportive seats.<br>
<br>
The rear seats are roomy and relatively
comfortable and the trunk is big
for this class. The Corolla rides and
handles well with a relatively soft suspension.
It isn't a sports sedan, but the
engine is smooth and sophisticated
and delivers good acceleration performance.
Toyota Corolla is the car to
consider if you're looking for a sophisticated
and utterly reliable sedan that
blends in so well with your life, it's
almost non-existent.<br>
<br>
