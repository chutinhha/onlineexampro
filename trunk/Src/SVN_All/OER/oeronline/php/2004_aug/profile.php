<p align="justify"><font face="Verdana" size="4" color="#FF0000">The dream 
merchant</font><font face="Verdana" size="1"><br>
<br>

<b>Shuchita Kapur talks to the young and effervescent Mohammed Al-Busaidi, GM, 
Eamaar Properties (Oman) about the challenges of running a business and climbing 
Mt Kilimanjaro</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im36.jpg" align="left" hspace="5" width="170" height="221">The future of Oman's enterprise is being shaped by the youth, filled with 
dreams, a passion for change, hope for the future and a desire to succeed.
One such personality that stands
out among this group of entrepreneurs
is that of Mohammed Al-Busaidi.
<br>
<br>
Very young, just at the age of 26, Busaidi is a name in the real estate sector
that has gained much prominence
over the past two years. Having been
involved in his family business since a
very tender age, Busaidi knows how to
make his perfect moves. Daring to start
new projects and taking up new challenges,
he is full of confidence and
determination. ''Being part of the family
business gave me the confidence of
dealing with problems in my own venture,''
he declares.
<br>
<br>
Busaidi isn't just confident, but also
believes in being absolutely independent.
After finishing his studies from the
UK, he came back and worked for the
State General Reserve Fund of the
Ministry of Finance. However, his new
job could not tie him up for long and he
left it in a year's time. After all, that wasn't
what he wanted. He wanted to set up
his own business, work by his own set
of rules and have his own system.<br>
<br>
And, today, here he is, the person at
the helm of affairs at Eamaar, a new
though promising company in Oman's
booming real estate sector. In a short
span of two years, the company has created
a niche for itself in the upcoming
sector, all thanks to Busaidi.<br>
<br>
<b>Learning his lessons </b><br>
Busaidi finished his schooling in
Muscat, after which he moved to the
University of Leicester in the UK for his
higher studies in accountancy and
finance. He is currently pursuing his
degree in chartered accountancy.
Having cleared the first and the second
stage, he will take examinations for the
third one this December. According to
Busaidi, pursuing business knowledge
at an early age is an important ingredient
for future success. &quot;My experience
with the family business gave me a lot
of confidence and taught me ways to
run a business successfully.&quot;
<br>
<br>
&quot;Even when I was studying in the
UK, I did not sit idle. I worked there too
and this gave a further boost to my confidence,&quot;
he adds. It was basically this
confidence instilled in him that gave
him the courage to start his own business.
''With the help of my uncles,
cousins and some very good friends, I
set up this company,&quot; he proudly
claims. ''There was a chance that this
company could have failed, but such
negative thoughts did not deter me
from my aim.''
<br>
<br>
''For me, one should face challenges
and not shy from them. I once climbed
Mt. Kilimanjaro in Africa, the highest
freestanding mountain in the world
and the highest peak in Africa. It wasn't
an easy task to cover some 17,000 feet
uphill, but it was a task which I was not
afraid of. Life itself is a challenge - you
face it and overcome the obstacles,'' he
says passionately. That's the mettle of
the man who successfully runs his own
real estate business in a very competitive
market, full of local, regional and
multinational firms. He displays mental
prowess that is not often seen in such
young minds and, unlike others, he
seems just invincible. 'Face and crack
problems, before they break you',
seems to be his punch line.
<br>
<br>
<b>Management mantra
</b><br>
Despite his tough mettle, Busaidi is far
away from being bossy. ''I believe that
my employees are not my subordinates.
It is a team and each member is equally
important. I do not take decisions all by
myself. I encourage my employees to be
a part of the decision-making process. I
never try to brainwash them with my
ideas. Our decisions are taken after we
have a through brain storming session
and everybody's ideas are welcomed,&quot;
he says.
<br>
<br>
For Busaidi, confidence is very
important - not just for himself, but for
his workers as well. ''If I let them decide
themselves, they will gain confidence,
which in the long run will give them the
assurance of managing things themselves.
Ours is a company with 100 per
cent Omani staff. It can be far easier for
me to recruit professional expats, but I
want my fellow countrymen to learn,
improve and achieve. And for this, I give
them the space they need,'' he maintains.<br>
<br>
<b>Countering challenges</b><br>
You mention the word challenge, and a
smile is the only expression that can be
seen on Busaidi's face. ''There are a lot
of challenges that we face, especially for
a new company like Eamaar. We have established competitors, who have been in 
this business for over 30 years now. So, it is definitely a challenge to stand 
up against them and beat them in business. My first big project was worth half a 
million. It was very successful, after which there was no looking back. But, for 
me, the biggest challenge is satisfying my customers. We remain in touch with 
our customers and while dealing with them, we always keep their requirements in 
mind and do not thrust our ideas on them,'' he explains.
<br>
<br>
''We believe in giving them what
they want, and if the exact thing is not
available, we offer them something
which suits them,'' he says. According
to Busaidi, real estate business does not
enjoy a very good reputation. He
believes there has been loss of honesty
and transparency in this sector. ''We
want our image to be very clean, and
we are very transparent in our dealings,
whether it is with our customers or
business partners. Reputation is very
important as only this will get customers
coming back to us,'' he says.<br>
<br>
<b>Foretelling the future</b><br>
According to Busaidi, real estate is a
developing sector in Oman with much
potential. The growth in this field is
slow, but it is becoming evident. Many
new developments have taken place,
especially big projects like The Wave,
which has made real estate look attractive
for business. ''There is so much
construction work going around. In five
years' time, there will be no land left for
construction in Muscat,&quot; he exclaims.
<br>
<br>
Overall, real estate has largely been
an untapped sector and everybody here
is to gain. It is a race in which Busaidi
does not want to be left behind. So, he is
trying to develop new ideas and projects.
''We are involved in big projects
and we provide basic service like electricity
and water to our customers when
we sell them land. We are also into construction
work, making flats to be sold.
This can be very profitable, now that
the government has allowed expats to
own property here,'' he says.
<br>
<br>
Moreover, Busaidi thinks that the
government's commitment to the
tourism sector will further push the real
estate sector to a greater growth trajectory.
''Tourism and real estate go hand in
hand. If tourism is promoted, real estate
gets a fillip. If tourist inflow increases, so
do the number of hotels and commercial
complexes,'' believes Busaidi.<br>
<br>
<b>Personal side</b> <br>
For this young man, a close knit family
is one of the greatest gifts one can have. Busaidi comes from a well-respected
family, his father being the directorgeneral
in the Ministry of Health and
mother working for Petroleum
Development Oman (PDO). He has
three sisters and one brother. He is a
happily married man with a baby girl.
Busaidi lives in a joint family and loves
the close relation which he shares with
each member of it.
<br>
<br>
Besides a loving family, Busaidi has
a big group of friends. ''I just love to
hang out with them. Just sit in a coffee
shop and talk to them and catch up
with each other's life. But it is really sad
that everyone is so busy in their own
affairs that there is not much time left to
be with each other. However, I make it
a point to meet them once a month. I
just wish I had more time to spend with
my family and friends,'' he laments.
<br>
<br>
Busaidi believes in keeping aside
business once in a while and get
involved in things which he is extremely
fond of, fishing being his favourite
pastime. ''I love fishing and camping.
This was something which I did a lot
before I went to England, but had to
forgo them during my stay there. But
now that I'm back, I have the privilege
of doing my favourite things again. I've
just bought a boat,'' he says gleefully.
<br>
<br>
That's not all, though. Fishing is not
the only activity that he is fond of. He
loves holidaying as well. ''I keep going
back to England to see my old friends,
but as a favourite holiday destination, I
like the Far East. I just love the place, its
weather, relaxing atmosphere, nice beaches…
it's just a home away from home,''
says a reminiscent young man.</font></p>

