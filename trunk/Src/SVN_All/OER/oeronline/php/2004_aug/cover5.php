            <p align="justify"><font face="Verdana" size="1">While financing such home purchases might be beyond the national 
            budgets of several African countries, it is clearly not out of some 
            people's reach. Helping people afford real estate in the UAE 
            recently has been mortgage company Tamweel, which announced as June 
            ended that in the last four months, it had provided some Dh500 
            million (RO52.41 million) of property financing in the Emirates.<br>
            <br>
            There is no doubt that the UAE economy as a whole has also benefited 
            from the construction and real estate boom. The most recent IMF 
            report on the Emirates showed that at current prices, the country's 
            GDP is likely to hit a record high of Dh310.8 billion (RO32.58 
            billion) this year - 5.2 per cent up on 2003. This would leave the 
            UAE as the third largest Arab economy, after Saudi Arabia and Egypt. 
            Many see it pushing the Egyptians out of second place too, over the 
            next few years. <br>
            <br>
            The strong growth rates are no historical blip either. From Dh263.4 
            billion (RO26.61 billion) in 2002, the UAE's GDP leaped by about 
            12.1 per cent to Dh295.4 billion (RO30.96 billion) last year. This 
            was one of the highest nominal growth rates in the world, according 
            to the IMF. <br>
            <br>
            Of course, hydrocarbons account for a giant part of the Emirate's 
            economic standings. Yet, the IMF report also forecast that the 
            non-oil economy would again be a dominant component in a very short 
            while. Oil exports are still heavily influencing the UAE's economy. 
            However, the UAE's economy is becoming one of the most diverse 
            economies in the GCC thanks to substantial diversification 
            programmes, mainly in Dubai. <br>
            <br>
            The evidence is certainly there on the ground - or rather, emerging 
            from the ground. Construction and real estate are targeting high-end 
            spenders and an expected surge in tourism. So far, it seems money 
            well spent, as both tourists and those staying longer term continue 
            to seek out property. Yet, it is no longer a new strategy in gulf, 
            with many other states seeking to emulate the success of the UAE - 
            and of Dubai in particular. Staying ahead of the game may turn out 
            to be the longer-term difficulty, but for now, the UAE seems to be 
            on solid, concrete ground. <br>
            <br>
            Activity in the nation's real estate sector was boosted further with 
            the implementation of a new law last year that allowed expatriates 
            to purchase properties in certain allocated areas. The initiative 
            has expanded as there exists a whole host of properties to choose 
            from, different finance schemes that are readily available and the 
            chance for foreigners to own their own property. Currently, there 
            are nine different property development projects available for 
            expatriate freehold purchase. The boom has not been limited to a few 
            emirates but instead has been across the entire nation. <br>
            <br>
            Dubai's real estate market was quite active and land prices rose by 
            approximately 27 per cent from Dh100 (RO10.48) per sq. mt to more 
            than Dh140 (RO14.67) per sq. mt. According to Aswar Real Estate Co., 
            there is strong demand for finished buildings, particularly those 
            with valuations ranging from Dh5 to Dh10 million (RO524,080 to 
            RO1.05 million). Demand for new buildings is largely dependent on 
            the returns generated by the property. <br>
            <br>
            While the demand for villas dropped in general, demand for luxury 
            villas in select areas remains strong. House rentals in different 
            residential areas are stable, except for villas where they are 
            seeing some decline. The Jumeriah area in specific has seen a bout 
            of frantic activity. The activity is in the form of buying out old 
            villas to make way for new townhouses and luxury villas. The Dubai 
            Marina project has received a good response.<br>
            <br>
            Abu Dhabi, another emirate, saw apartment rents surge by more than 
            20 per cent in 2003 over 2002. The upward trend is expected to 
            continue in the near future as building owners are taking advantage 
            of the absence of official curbs to raise rents, and demand for 
            accommodation and offices remains robust due to a steady growth in 
            non-oil businesses. Ajman, a city within the Al Sharjah emirate has 
            also witnessed increase in land prices, but for a different reason 
            altogether. Land owners were asked to register properties that were 
            going to be connected to the new sewage system, which in turn raised 
            the price of plots of land. <br>
            <br>
            Nonetheless, the area has seen an improvement in the local market 
            following the end of the war in Iraq which has encouraged more and 
            more investors to invest funds into the emirate. Demand in Al 
            Sharjah in general has been stable, but it is slightly shifting 
            towards industrial and commercial land. Investment returns from real 
            estate sector projects in UAE are among the highest in the region, 
            because rent is high and building costs have gone down. Therefore, 
            increased activity in the market can be expected throughout 2004.
