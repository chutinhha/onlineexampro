            United Arab Emirates (UAE) has seen a substantial and remarkable 
            increase in its real estate market activity. Figures from the 
            Central Bank show that the share of real estate in GDP peaked at 
            around Dh20.3 billion (RO2.13 billion) in 2002 to become the third 
            largest sector after oil and manufacturing. <br>
            <br>
            <img border="0" src="im19.jpg" align="left" hspace="6" width="200" height="166">With deals worth over Dh1 billion (RO105 million) taking place on 
            the Dubai property market in the month of June 2004 alone, these are 
            expansive times for construction and real estate in the emirate. 
            This flurry of activity also backs up the argument that the UAE is 
            set to see some impressive growth this year, particularly in its 
            non-oil sector. Following this viewpoint, the IMF forecast last 
            month that non-hydrocarbons would return as the dominant economic 
            motor force for the Emirates in the year ahead. <br>
            <br>
            The Dubai Lands and Properties Department recorded some 256 deals in 
            June, worth a total of Dh1.02bn. Al-Warqa 3 came top in the table of 
            most deals, with 16 contracts, followed by Mirdiff, where 15 plots 
            were sold, and Nad al-Sheba, with 11 deals. The month ended with a 
            bang too, as the last week saw the biggest deal in the past six 
            months - Dh195 million (RO20.44 million) for a residential tower on 
            Sheikh Zayed Road in Dubai. <br>
            <br>
            Hectic activity was how many analysts 
            described the scene on the property market, and this seems no 
            exaggeration. July also saw news that property developer Nakheel had 
            awarded al-Sahel Contracting a contract to build 27 commercial 
            buildings at International City, a new 800-acre residential and 
            commercial project being developed in Dubai's al-Warsan district.
            <br>
            <br>
            Including a replica of Beijing's Forbidden City, International City 
            is composed of a number of building projects, with residential units 
            in a variety of different national styles and a comprehensive 
            assortment of other facilities, from malls to entertainment centres.
            <br>
            <br>
            At the same time came another announcement that Hong Kong's Trident 
            International Holdings (TIH), had awarded the construction contract 
            for Phase II of its maiden project, The Waterfront, to Larsen &amp; 
            Toubro Ltd (LTL), one of India's largest engineering and 
            construction conglomerates. <br>
            <br>
            The Waterfront is another grand project, consisting of a 20-storey 
            super luxury tower in the exclusive Dubai Marina. Inside the tower 
            are 72 smart homes, varying from the plain luxurious to the 
            outstandingly luxurious, and all of which have already been sold.
            </font></p>
            <p align="center">
            <img border="0" src="im20.jpg" vspace="4" width="350" height="96"><br>
