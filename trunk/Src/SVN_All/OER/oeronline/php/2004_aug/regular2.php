<p align="justify"><font face="Verdana" size="4" color="#FF0000">The death of shareholder value</font><font face="Verdana" size="1"><br>
<br>
</font><b><font face="Verdana" size="1">
<img border="0" src="im44.jpg" align="left" hspace="5" width="150" height="184">As shareholder value comes increasingly under scrutiny, new business drivers 
that are more ethically
founded are likely to emerge as the true measure of a company's performance, 
writes Ali Hasan Moosa </font></b><font face="Verdana" size="1"><br>
<br>
While shareholder value has
become a widely used cliche, it
is, perhaps, not as widely
understood. This may be
because different measures of
shareholder value capture different aspects of
a company's performance. The technical definition
of shareholder value is as follows:
<br>
<br>
<b>Net Present Value (NPV) of future cash
flows + value of non-operating assets -
future debt claims
</b><br>
<br>
Shareholder value is created by earning a rate
of return on invested capital that exceeds the
firm's weighted average cost of capital.
Another way to look at it is that shareholder
value is created when a company generates
cash flows in excess of shareholders' investments
in the business. While management and
investors both measure the value created, it is
the management that creates value by developing
and implementing an appropriate business
strategy.
<br>
<br>
One of the main components of shareholder
value is the share price. It may, therefore, be
worth analysing what a share price actually
reflects. In an efficient market and at any point
in time, the price of a stock represents a fair
view of the company's future earnings plus
the initial equity investment.
<br>
<br>
Too high a share price can be as damaging
as under-valuation; when a share price correction
takes place in the market, company executives
can be accused of destroying shareholder
value. However, in reality, the market may
have over-priced the share to begin with, and
could be under-pricing it eventually. Nothing
may have changed in the fundamentals of the
business - it is all a question of perception.
<br>
<br>
Some investors tend to focus purely on the
narrow prospective of share price and market capitalisation (no. of shares outstanding x current
share price). Other investors may have a more
pragmatic view of Total Shareholder Return
(TSR) as the ultimate measure of success. Total
Shareholder Return can be interpreted as:<br>
<br>
(Change in share price + Dividends
received) / Original cost of share price
<br>
<br>
The debate is that do firms have a duty to maximise TSR via dividend payouts and
increase in market price of its shares?
<br>
<br>
Such an approach could be disastrous to
the company and its shareholder value over
the long term. The dotcom era came to a terrible
end as investors assumed high share prices
meant that those companies were doing well.
The high profile collapse of companies in the
US such as Enron and WorldCom and the
downfall of some SAOG (Society Anonym
Omani General) companies and banks in
Oman were examples that growth in shareholder
value via TSR method while ignoring
other fundamental operational and strategic
issues have failed to produce sustainable
growth in shareholder value.<br>
</font><font face="Verdana" size="1"><br>
Although one must not only question the
integrity of failed company management, but
also directors and shareholders who, on many
instances, may have relied solely on easily
manipulated performance measures, overreacted
to market news, and tolerated long-term
corporate strategies for easy capital gains on
the share price and/or maximum possible dividend
payouts.
<br>
<br>
As a result, a large group of investors
worldwide call for the death to the shareholder
value concept. They insist that shareholder
value has encouraged some company directors
and executives to purely focus on shortterm
earnings, inflate share price and escape
with hefty cash and stock option bonuses.
<br>
<br>
Critics of shareholder value accuse the
concept as morally bankrupt because the philosophy
exclusively revolves around corporate
ownership. Any obligation towards staff,
customers, the community and the environment
is only seen through the lens of how it
adds value to shareholders.<br>
<br>
It is appalling to note some executives who
have used the shareholder value mantra to
justify their actions in terms of exorbitant
numeration schemes for management and
business acquisitions that have enlarged corporate
power base but without adding noticeable
value to shareholders.
<br>
<br>
Shareholder value concept was useful for
companies who were insufficiently focused on
the bottom line, cost reduction and share
price, particularly in an economic environment
where the decline of share price may
lead to a hostile takeover.
<br>
<br>
While a few great companies have set
themselves very high standards for having
distinctive strategies and decision-making
processes, many others have executed the
shareholder value mandate wrongly.
Common mistakes include excessive focus on
share price and dividend payouts and too little
focus on growing long-term intrinsic value.<br>
<br>
Erroneous execution of shareholder value
mandate has been evident in the local economy
as some SAOG companies have had volatile
stock price and/or excessive dividend payouts,
some of which were not able to sustain
shocks and went either bankrupt or had their
shareholder value severely damaged. It is also
noticeable that some companies in Oman have
solely rewarded their shareholders through
dividend payouts while their stock prices
remained static over the medium to long term.
<br>
<br>
In comparison, Fortune 500 companies
have hardly satisfied their shareholders
through dividend payouts. Their strategies
revolve mainly around long-term sustainable
growth of the business that is reflected in the
long-term growth of the stock price.<br>
<br>
It is widely believed nowadays that shareholder
value itself cannot be treated as a concept
to create value, as it does not create ideas
about services and products, nor does it
inspire staff for greater efficiency especially
when cost reductions are at their expense.<br>
<br>
Shareholder value is becoming increasingly
questioned as the leading business slogan.
The backlash against the concept is ever
increasing and we are likely to see in the next
few years new business drivers that are more
ethically founded.
<br>
<br>
Future corporations will be required to
demonstrate how they make the world a better
place to live, not only for shareholders, but
also for their staff, customers, competitors and
the community at large.
<br>
<br>
To summarise, short-term indicators such
as earnings, share price, earnings per share
and dividend yield may not necessarily say
anything about shareholder value, especially
when they are examined in isolation. The key
is to base long-term growth on intrinsic value
of the firm. As Warren Buffet says &quot;Intrinsic
value is the only logical way to (manage)
investments and business&quot;.
<br>
<br>
