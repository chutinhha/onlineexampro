            </font><font face="Verdana" size="1">Sultanate of
            </font><font face="Verdana" size="1">Oman has witnessed a surge in
            </font><font face="Verdana" size="1">real estate activity 
            since 2002. The total value of the real estate sector in Oman 
            increased by 78 per cent to around RO737 million in 2002 as compared 
            to RO414 million in 2001. The sultanate issued 24,273 land ownership 
            licenses in 2002. Further, 30,038 properties were registered for the 
            first time compared to 22,357 first time registrations in 2001. <br>
            <br>
            In specific, the capital city Muscat, has seen relatively high 
            activity in real estate especially in the property rentals market. 
            Oman has also attracted GCC investments especially from UAE and 
            Kuwaiti nationals. About 300 Kuwaiti nationals had purchased 
            properties in Oman as of end 2002. In addition, UAE based companies 
            have invested millions of dollars in real estate projects across the 
            country. <br>
            <br>
            GCC investors are particularly interested in commercial and 
            investment plots and this interest is expected to increase as the 
            investment will be aimed at the development of hotels and tourist 
            resorts. It is also worth mentioning here that Oman has positively 
            ratified many commercial and industrial regulations including 
            regulations dealing with the real estate sector which had an 
            encouraging effect on the real estate market in Oman.<br>
            <br>
            Activity is relatively high in Muscat's property rental market with 
            both landlords and tenants often surprised by the pace at which a 
            good property is let. The past few years have been a rollercoaster 
            ride for this sector that started on a quiet note but gathered pace 
            since the summer months of 2002. In Oman, incoming expatriates drive 
            the market and capital values to a large extent, particularly in 
            attractive areas like Shatti Al Qurum. Although the amount of space 
            is increasing, the overall number of tenants has not risen 
            significantly, thus leaving the market fairly flat. <br>
            <br>
            There is consistent talk in Oman about people leaving the country 
            but there is less said about those arriving into town. In the upper 
            end of the property rental market, Muscat has seen large number of 
            arrivals in the past two years. Although the rate of Omanisation in 
            the workplace is increasing, a lot of these arrivals are replacing 
            expatriate workers who have left the country. Put simply, it isn't 
            that the overall ratio of foreigners has gone up but, evidently, 
            more job opportunities are being created in the economy. <br>
            <br>
            There has been a noticeable demographic change due to the new 
            arrivals who have entered the mid-to-high end of the market. We have 
            seen a shift towards more singles, young couples and older couples 
            rather than middle-aged couples coming into the Gulf. The main 
            reason for this could be that salaries in Europe and the US have 
            been steadily rising leaving fewer reasons for expatriates from 
            these areas of the world to work in the Gulf. Also, a lot of 
            families with young children probably don't necessarily feel secure 
            in the Gulf in the current geo-political climate although Oman is 
            one of the safest places in the world to live in. <br>
            <br>
            The two-tier market dividing sought-after properties that meet 
            tenancy specifications and poorly looked after buildings which tend 
            to lie idle has been accentuated further in the past year. Since 
            February 2002, rents have remained more or less static. In fact, in 
            the past four years, there has been no significant fall in rents 
            across Muscat barring the strengthening of rents in areas like in Al 
            Ghubrah and Al Athaiba. The availability of new accommodation, 
            proximity to good beaches and the ever-expanding facilities in these 
            areas has made them the 'hot spots' of Muscat. New or 
            well-refurbished property in these areas tends to lease 'before it 
            comes to market' reflecting the appeal these areas have.<br>
            <br>
            However, there isn't any substantial new demand in the market. 
            Basically, people are moving from one place to another. It's still a 
            tenant's market. The supply is much greater than demand, so the 
            tenant is in a good position to negotiate and can often move into a 
            better building without having to worry about an increase in rent. 
            There are some exciting property development schemes being developed 
            at the moment. <br>
            <br>
            <b>A Wave of opportunities </b><br>
            <img border="0" src="im9.jpg" align="left" hspace="6" vspace="3" width="250" height="90">In February 2004, the Omani government--in an attempt to woo 
            investors and tourists alike--unveiled its latest project - The 
            Wave, a RO310 million seafront resort. The plan calls for the 
            building of a marina, a five-star spa and beachfront hotel, premium 
            villas, and a golf course on 7.3 km of beachfront just west of 
            Muscat. The Wave will be the most ambitious tourist project 
            attempted in the country. <br>
            <br>
            Officials hope that The Wave's golfing and conference centre will 
            attract businessmen as well as tourists. In addition, permanent 
            residents are being courted to buy the Wave's 505 residential units. 
            Two other resorts--the Barr-al-Jissah Resort and the Muscat Golf and 
            County Club--also provide ample private housing. Large-scale resorts 
            are also being planned near Salalah in Dhofar. <br>
            <br>
            But The Wave still lacks investors, and until they arrive, the 
            project will remain only a colourful drawing. While the government 
            claims to have received many offers, none have been finalised or 
            made public. This reticence can be partly explained by the size of 
            the project. The logistics are daunting. The first stage is the 
            development of 2 million sq metres of al-Athaiba/al-Hail coastline, 
            weighing in at a hefty RO32 million. Only after the land is reshaped 
            will any actual building take place. <br>
            <br>
            Local architects are concerned that the project is just too risky 
            and too expensive. With no market yet to fill these 1,000 new 
            residences, investors are hesitant - especially with the high costs 
            of reshaping the land. If the government sank the initial RO32 
            million to develop the land, there would be a greater sense of 
            shared responsibility. <br>
            <br>
            The fact remains that the current number of visitors to Oman is not 
            high enough at the moment to warrant the building of such a project. 
            The government is depending on a drastic increase in tourist 
            arrivals over the next 15 years and wants to be prepared for the 
            influx. Oman is relatively new to the tourist market. Following 
            years of seclusion from the rest of the world, the country only 
            began to open its doors to tourists in the mid-1980s. Since then, it 
            has proceeded cautiously, targeting niche markets like eco-tourism 
            and adventure tourism. <br>
            <br>
            However, there are plenty of reservations also being expressed as to 
            what this type of tourism brings. Unlike Dubai, which encourages 
            tourists from any background, Oman has traditionally targeted 
            wealthier Europeans. These visitors--usually composed of smaller 
            groups--have generally been thought to possess a greater sensitivity 
            to the more conservative aspects of Omani society. Specifically, 
            they are less likely to cause the problems with alcohol usually 
            endemic in large resorts and on package tours. <br>
            <br>
            There are also issues surrounding who will buy the residential parts 
            of The Wave. Since the Omani government provides land for its 
            citizens, it is unlikely that Omanis will jump at the prospect of 
            expensive, resort-style housing. <br>
            <br>
            The onus then falls on foreigners. A new decree from His Majesty 
            Sultan Qaboos has taken steps to allow residents of the GCC to rent 
            and own real estate in Oman. There are rumours that a law is in the 
            works to allow non-GCC foreigners to own land as well. Yet, until 
            the specifics are ironed out--for instance, the sticky issue of 
            obtaining permanent visas for foreign land holders--property 
            investors may not be as forthcoming as the government would want 
            them to be. <br>
            <br>
            Moreover, even if laws were to soften, it remains to be seen whether 
            expats would find projects like The Wave attractive. Its proximity 
            to nearby Seeb International Airport may make transport easy, but 
            potential residents may want more distance from such a busy hub. 
            Plus, with a nascent real estate market offering new two-bedroom 
            apartments for around RO45,000, buyers may look elsewhere to invest.
            <br>
            <br>
            The government, however, is as optimistic as ever. The burgeoning 
            development of the tourist sector promises to be a boon to the local 
            job market. The Wave project alone ''is likely to create around 
            2,000 jobs. More young Omanis are heading towards the hospitality 
            industry,'' said His Excellency Abdullah bin Abbas bin Ahmed, Head 
            of Muscat Municipality and Chairman of the Oman Tourism Development 
            Company, in a recent statement. <br>
            <br>
            Muscat Oasis is another noteworthy residential and commercial 
            complex. Currently under construction, the US$30 million (RO11.11 
            million) project headed by Sheikh Salim Nasser Alssiyabi aims to 
            provide 400 two- and three-bedroom luxury apartments by November 
            2005. The amenities planned include swimming pools, a children's 
            outdoor play area, a gymnasium and sports/social hall, a tennis and 
            basketball court, a family supermarket, a pharmacy, a clinic, and a 
            24-hour crèche and nursery.
