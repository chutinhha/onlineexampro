            third quarter. As per the data available from the land registry 
            directorate of Bahrain, there has been a consistent rise in real 
            estate transactions since the past few years. Because of the lack of 
            high yielding investment instruments, excess liquidity, and overall 
            political stability, the real estate market grew to $615.4 million 
            (RO236.69 million) in the first quarter of 2003, as compared to 
            $468.9 million (RO180.35 million) in 2000. <br>
            <br>
            Despite the increasing activities in the real estate sector, there 
            exists extreme shortage of housing in Bahrain especially in the low 
            to medium income family range. The number of total applicants to the 
            Ministry of Housing outnumbers the number of loans given by the 
            housing bank in Bahrain. In 2001, around 6,500 applications were 
            received by the Ministry of Housing while around 2,300 were given 
            loan by the housing bank. The figures confirmed that there is a 
            demand for residential houses in Bahrain and there remains a huge 
            gap that needs to be filled. As a result, the Cabinet approved an 
            additional support of BD12 million (RO12.26 million) to the Housing 
            Bank.
