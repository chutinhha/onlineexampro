            This law sets out areas where non-Bahrainis can own land, property 
            and the type of property. Bahrain has witnessed high demand for 
            residential property from people working in Saudi Arabia who are 
            based in Bahrain. In its quest to establish the island Kingdom as 
            the regional 'family tourism' centre, the Bahraini government is 
            offering permanent residence to expatriates who buy real estate in 
            one of the new development projects.<br>
