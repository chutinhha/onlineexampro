            with their companies can move their families to Bahrain and continue 
            working in the other Gulf country, according to a plan introduced 
            recently by the government. The property market in Bahrain is set to 
            grow exponentially in view of the number of high profile projects in 
            the pipeline coupled with increasing foreign investment, excess 
            liquidity and government's liberal policies towards non-Bahrainis to 
            own property in the country.
