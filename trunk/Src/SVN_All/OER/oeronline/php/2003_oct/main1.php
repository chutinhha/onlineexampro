            <p><font face="Verdana"><font size="4" color="#FF0000">Defence 
            Against The Dark Arts</font><br>
            <br>
            <b><font size="1">Vicky Kapur takes you through the world of 
            hacking, competitive intelligence and cyber underworld to make you 
            wizardly&nbsp; wise</font></b></font></p>
            <p><font face="Verdana" size="1">
            <img border="0" src="im34.jpg" align="left" hspace="5" vspace="5" width="250" height="163">International 
            spies, pilfered documents, treason, deceit, treachery... Sounds like 
            vestiges of the cold war era? It could be happening in your 
            organisation - right now. Even as you read this, black magic wizards 
            of the cut-throat corporate world are busy digging information, 
            statistics, data, facts, figures, numbers, records, titbits... 
            anything and everything about your company they can lay their hands 
            on to gain competitive advantage or, worse, inflict definitive 
            damage.<br>
            The threat of espionage is often associated with political 
            activities, used as an excuse for internal suppression and the 
            suspension of civil rights. It has, indeed, been an important part 
            of guerrilla warfare and counterinsurgency. For instance, 
            high-technology ground equipment and high-altitude planes and 
            satellites were used for political espionage during the Cuban 
            missile crisis.<br>
            Espionage has become an important technique to defeat the enemy by 
            getting advance information about its plans and defences. The FBI 
            and the US intelligence use it to track their most wanted. However, 
            the use of espionage is not limited to political activities alone. 
            Intelligence professionals are now focusing their black-bag wizardry 
            on big business houses, giving rise to one of the most threatening 
            crimes of the modern era.<br>
            Welcome to the world of corporate espionage, where there are no 
            rules, no standards, no ethics. Any information your rival unearths 
            can be and will be used against you. And the means that the 
            perpetrators of corporate espionage employ aren't space-age gadgets 
            or supersonic aircraft - it could be as basic as the everyday trash 
            from your corporate bin.<br>
            Moreover, the culprit here is no Voldemort 
            (the-one-who-must-not-be-named of Harry Potter fame) - it could be 
            someone typing away on the network PC in the cabin next to yours. 
            Incredibly, what makes it perilous isn't the fact that you may not 
            know about it - it is the truth that the person leaking the 
            information may not be aware of it either. <br>
            Security of information is fast becoming one of the most important 
            concerns of businesses. Risks increase as businesses go online, 
            expand globally or involve working with partners. In a competitive 
            environment, even a slight edge is the difference between success 
            and failure.<br>
            <br>
            <b>What is corporate espionage?</b><br>
            Put simply, corporate espionage can be summed up as activities by 
            individuals, domestic companies and foreign governments and 
            corporations to illicitly obtain the information assets of another 
            business for competitive advantage. This formal and organised 
            competitive intelligence gathering that has intensified in the 
            recent past because of the use of new techniques and technologies. 
            If you may recall, a skinny kid with coke-bottle glasses hacked his 
            way through a multi-billion dollar computer system. <br>
            While that incident of juvenile hacking was played full by the 
            media, kids aren't the real threat. And it isn't the traditional 
            security leaks but the novel hack attacks that make corporate 
            espionage so lethal. &quot;It is an ever-present threat, and you can 
            never be 100 per cent secure,&quot; says Kannan Murali, operations 
            in-charge at a Web development firm in Oman. <br>
            In the age of the Internet and global economy, it gets more and more 
            real every day. And the threat is closer home than you think. It may 
            come as news to some that, during an underground hacking competition 
            in late August, the Website of one of Oman's leading 
            government-owned institutions got hacked. Luckily, the hackers were 
            not interested in causing more damage than replacing its homepage 
            with their signature slogan, something that the organisation's 
            technology team was able to rectify quickly.<br>
            However, had they intended, the hackers could have caused much more 
            extensive damage that could have cost the corporate heavily in terms 
            of data and man-hours lost. Espionage is definitely something to 
            beware of and get counter-measures in place ahead in time. &quot;Such 
            incidents prompt organisations to acknowledge their vulnerability,&quot; 
            says Capt. Raghu Raman, Global Practice Head, Special Services Group 
            (SSG), Mahindra Consulting Ltd (MCL), a US-headquartered 
            organisation specialising in developing de-risking strategies for 
            enterprises across various domains.<br>
            <br>
            <b>Why corporate espionage?</b><br>
            Information is a valuable asset of any company. It could be in the 
            form of, among other things, trade secrets, client lists, supplier 
            agreements, personal records, research documents or prototype plans 
            for new products or services. These are highly confidential details, 
            and if such information is leaked or stolen, it can adversely impact 
            the business and profits of the victim company.<br>
            On the other side, this piece of information can work wonders for 
            the competitor's business as not only will it be able to take steps 
            in time to counter any threats arising out of the fulfilment of the 
            victim's proposed plans, but also launch new products and services 
            based on the leaked information before the victim. <br>
            One such case came to light when, in August 2001, Procter &amp; Gamble 
            (P&amp;G), a global giant in the cosmetic and personal hygiene sector, 
            admitted to hiring corporate spies against Unilever to gather 
            information on its haircare business. The spies, who are said to 
            have misrepresented themselves as market analysts, are believed to 
            have learnt a number of secrets about Unilever's haircare business, 
            which P&amp;G wanted to use to bolster its own brands. However, after 
            the incident came to light, P&amp;G and Unilever entered into an 
            out-of-court settlement, which some speculate could have entailed 
            P&amp;G paying up to $50 million (RO19.23 million) to Unilever.<br>
            Another case worth mentioning here is of an Indian drug company, 
            which was involved in research of a new product. After having 
            invested millions of dollars in the research work for a couple of 
            years, the company finally got the formula of the new drug. However, 
            before the company could come out with its new product in the 
            market, the formula was leaked to a rival organisation, which 
            immediately got its copyright by launching it first.<br>
            It isn't just rival organisations engaged in espionage. Even 
            countries are involved in this crime. One such case was exposed 
            when, in 2001, the EU accused America of industrial spying and 
            illegitimate economic intelligence gathering. A committee of the 
            European Parliament approved a resolution asking the US government 
            to stop its long-denied Echelon spy network from targeting its 
            member countries. <br>
            On the flip side, in a report in USA Today, Louis Freeh, the-then 
            director of the FBI, defended his country by portraying a vulnerable 
            image against the outside parasites. &quot;...roughly 23 countries from 
            around the globe regularly prey on US business for the competitive 
            advantage they can acquire. These corporate spies, who infiltrate 
            companies by posing as workers, and bribe current and former 
            employees, are the most severe threat to national security since the 
            Cold War,&quot; he had said.<br>
            By providing the local organisation with a competitive advantage, 
            foreign governments are attempting to gain a national economic 
            competitive advantage over the country in which the targeted 
            organisation is located. While these activities may sound 
            far-fetched, consider that Hitachi paid IBM a reported $300 million 
            (RO115.38 million) in a settlement agreement after being caught 
            spying on a new generation of IBM computer equipment and that French 
            intelligence was proven to have spied on Boeing to help Airbus, a 
            French airplane manufacturer, compete against a new generation of 
            Boeing airplane, the 747-400. <br>
            <br>
            <b>Who's the wolf?</b><br>
            The culprit could be anyone, right from a teenager out there to 
            prove his mettle among his peers to corporate blue-chips looking to 
            gain any and every bit of competitive advantage they can. One of the 
            richest men in the world, Bill Gates, was once a hacker. 
            Incidentally, there are good hackers and there are bad hackers. Bad 
            hackers are ones who deface a Website with electronic graffiti, 
            flitch usernames and passwords or steal credit card information to 
            carry out financial frauds. Good hackers, on the other hand, are 
            those who use their hard-sought knowledge in legitimate ways.<br>
            However, the margin between the two is wafer thin, firstly, because 
            a good hacker might be bad for another company and secondly, good 
            hackers mainly rise in life with a background of bad hacking. Many 
            hackers are employed by big companies to keep other hackers at bay. 
            Even if they don't end up hiring them, blue-chip companies like 
            Microsoft and Novell have held discussions with hackers to help 
            figure out how to plug security holes in their products.<br>
            It isn't only hackers, but even undercover secret agents that can 
            gain access to confidential information in your organisation through 
            myriad means. They could pose as students researching for a 
            university thesis, media personnel reporting for a story, 
            headhunters with a lucrative job offer...practically, anything that 
            will have you lower your or your staff's defences. &quot;They are great 
            liars, misrepresent themselves unbelievably, steal phone records and 
            do anything they can to wiggle their way to get invaluable 
            information from and for companies,&quot; says MCL's Raman. <br>
            &quot;You'll be surprised to find how much confidential information can 
            be pilfered by routine efforts - looking at innocuous-looking memos, 
            sifting through office trash, peeking on desktops, or simply asking 
            for it,&quot; exclaims Raman. Besides, the Internet has made it so much 
            easier to gain access to information. &quot;The fact that needs to be 
            driven home is that it isn't any James Bond who is perpetrating this 
            high-tech crime - tonnes of money is lost every year by companies 
            because their valuable information leaks through ordinary people and 
            preventable methods,&quot; insists Raman.<br>
            Raman, who was in Oman recently to give a presentation to a couple 
            of banks, financial institutions and an airline, explained to OER 
            how they go about pilfering classified information about a company 
            (of course, with the organisation's permission) to identify their 
            vulnerable areas in order to strengthen the system to avoid future 
            leaks (see box How it works). <br>
            <br>
            <b>The cost of espionage</b><br>
            Earlier this year, in a report to European Parliament, a British 
            investigator asserted that both US and European companies routinely 
            engage in corporate espionage. And many foreign corporations 
            regularly receive help from intelligence-gathering networks in their 
            own governments, which use the latest in information monitoring 
            technology to keep abreast of supposedly private Web communiqués. 
            According to the US Chamber of Commerce, corporate espionage costs 
            US shareholders at least $25 billion (RO9.61 billion) a year in 
            intellectual property losses.<br>
            According to statistics released by CERT's (Computer Emergency 
            Response Team) Coordination Centre, the number of reported network 
            security violations to CERT has gone up from 773 in 1992 to 82,094 
            in 2002. The year 2003 has already seen 76,404 reported violations 
            in the first half. Besides, a single incident may involve one site 
            or hundreds (or even thousands) of sites. Also, some incidents may 
            involve ongoing activity for long periods of time. <br>
            And this definitely is the tip of the proverbial iceberg as there 
            are many such associations in cyberspace that offer technological 
            first-aid. Moreover, says Murali &quot;A vast majority - maybe 75 per 
            cent - of security breaches go unreported.&quot; Add to that the number 
            of attempted but unsuccessful violations, and it is up to the 
            companies to bolster their defences against the relentless 
            bombardment they face.<br>
            The importance of protecting information can be gauged from a public 
            statement made by the US' Department of State: &quot;Proprietary 
            information is the chief competitive asset of any organisation, 
            vital to both our industry and our society. Our livelihood and, 
            indeed, our national strength depend on our ability to protect 
            industrial and economic data.&quot; Needless to add, the statement holds 
            true for any country or society. <br>
            <br>
            <b>Countering the danger</b><br>
            MCL's Raman suggests a three-pronged approach to 'securitising' a 
            corporate: Inspect, identify, indemnify.<br>
            Inspect the processes: Examine in detail the information-handling 
            routines followed by an organisation. This must not ignore innocuous 
            looking details like where does that 'stick-on' with a confidential 
            note scribbled on it eventually land - perhaps in the trash can, 
            along with other immaterial stuff. <br>
            The day-to-day operations of any company include various processes 
            accomplishing various functions. Each process involves information 
            handling at some stage or the other. There is a vital link between 
            the safety of information and the accomplishment of the function 
            that relies on that information. The three aspects of information 
            security are: Confidentiality, ensuring that the information is 
            accessible only to those authorised; Integrity, safeguarding the 
            accuracy and completeness of the information; and Availability, 
            ensuring that authorised users have access to the information 
            whenever required.<br>
            Identify loopholes: After scrutinising every individual process 
            which handles any kind of important/confidential information flow, 
            the next step is to identify possible sources of data leakage. &quot;An 
            unwitting employee who downloads a game or application that contains 
            harmful code is a serious insider threat,&quot; points out Raman.<br>
            Indemnify the system: Once all the loopholes have been recognised, 
            the final and obvious step is to ensure that they are plugged 
            without further delay. For this, Raman suggests that the gaps be 
            categorised as technological, procedural and manual. &quot;Only after the 
            loopholes are classified can the remedial process begin.&quot; And that 
            includes rectifying gaps in technology viz. fax machines, mobile 
            phones, building and/or bolstering firewalls, hardening corporate 
            networks (LANs and WANs), using cryptographic services such as 
            encryption and digital signatures to protect information integrity 
            while maintaining message authenticity and guaranteeing 
            non-repudiation etc. <br>
            Then comes the procedural part of it, which includes preventing 
            information leaks of the kind mentioned in <br>
            the How it works box. Another example that Raman provided was in 
            case the services of an expatriate employee are terminated. Between 
            the time the organisation's human resource department serves the 
            minimum three weeks' notice and the systems department denies the 
            individual access to the network after the expiry of the notice 
            period, the employee has the chance, if s/he is so inclined, to mess 
            about with the company's vital records. <br>
            &quot;Neither the HR nor the technology department are 'wrong' in the 
            processes they are following, but there is obviously a loophole 
            between the two processes that can be and is exploited at times,&quot; 
            emphasises Raman. It is the innumerable loopholes like these that 
            must be plugged, he adds. As Murali puts it, &quot;Internal attacks are 
            the most difficult attacks to prevent and shield from.&quot;<br>
            The most extensive and intensive part of indemnifying the system is 
            the people part. &quot;Staff at all levels need to be trained in 
            information handling,&quot; says Raman. It ranges from the senior 
            management (who handle data critical to competitive intelligence on 
            a regular basis) to the operational functionaries (who piece 
            together this information for the senior management and thus must be 
            aware of the risks involved and be up-to-date with information 
            security systems and follow best practices) down to the systems 
            people (who ought to be experts in limiting the scope of damage done 
            by creating the necessary checks and balances like password 
            protection at every level).<br>
            However, the fact remains that the establishment of a completely 
            secure organisation is a misnomer. There will always be some 
            weakness to be exploited by a clever competitor and the defender 
            must be constantly alert..</font></p>
