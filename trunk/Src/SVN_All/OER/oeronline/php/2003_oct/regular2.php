            <img border="0" src="im56.jpg" align="left" hspace="5" vspace="5" width="250" height="143">The 
            global and local track record of Toyota Motor Corp. is awesome. The 
            number one exporting company in the world and third largest 
            automobile manufacturer in Japan has, for the past 66 years, made 
            over 117 million vehicles worldwide selling 5.5 million Toyotas last 
            year (global sales figures that increase to 6.2 million if you add 
            group companies Daihatsu and Hino). The company's total sales level 
            across all segments within the GCC region in 2003 is expected to 
            exceed 220,000 units (a rise of nearly 13 per cent from sales it 
            achieved in 2002). Moreover, Toyota firmly remains the undisputed 
            auto market champion in the Sultanate under the guidance of its 
            local dealership Saud Bahwan Automotive LLC. Today, the Japanese car 
            maker's foray into the hatchback market could prove very lucrative. 
            In Oman, last year, around 1,600 units in this segment were 
            imported; a market currently led by the Volkswagen (VW) Golf and its 
            sibling the Polo as well as the Peugeot 206. What's more, volumes 
            look set to grow significantly in the next few years, as 
            increasingly more youngsters become customers at Oman's car 
            showrooms. <br>
            So how does the all-new xA fare? At a special press meeting held in 
            Muscat last month, high-ranking Toyota and Saud Bahwan Group 
            officials told journalists that they were launching much more than a 
            conventional compact urban car and that &quot;in many areas such as 
            power, performance, safety and comfort, it is one of a kind&quot;. In 
            particular, Tetsuya Tada, chief engineer, Toyota xA, expressed his 
            complete satisfaction with the hatchback, saying, &quot;We are confident 
            that we have succeeded in developing a car that can meet and answer 
            the versatile demands of youth and satisfy their sense of values.&quot; 
            He told OER that the xA's initial design began two years ago and 
            that most of the original designs he had drawn up at the time had 
            survived in tact as the car moved into large-scale production. &quot;With 
            distinctive styling, high-quality materials and exciting 
            performance, this new Toyota will exceed customer expectations and 
            receive an enthusiastic response,&quot; he added. And, in all fairness, 
            the xA seems to be a trendsetter that is sure to grab the interest 
            of young Omanis and, more importantly for such customers, it 
            delivers a decent amount for it's price tag which is upwards of 
            RO4,000. An affordable premium product, then, for first-time buyers 
            aged 18 - 25 (men as well as women) as well as for young-at-heart 
            drivers who want an additional fun-to-drive, run-around-town ride.
            <br>
            <br>
            <b>Outdoors</b><br>
            All in all, Toyota has created a good-looking car. The chassis of 
            the xA has a very contemporary feel, as one would expect. The rear 
            has oodles of athletic curves which radiate from a shapely bumper. 
            In front, the hatchback has an appealingly wide grille and is 
            snub-nosed. The front lamps are inset and shaped like a 
            parallelogram while the rear lamps are similarly shaped but 
            coordinated to fit with the wide arched SUV-like rear door. There 
            are plenty of pleasing sloping aesthetics too such as the xA's 
            roofline and bonnet. Muscular wheel arches extend to the vehicle's 
            four corners (akin to a VW Beetle) and the large diameter tyres also 
            embrace the cheeky charms of a sporty SUV. Yet there is enough 
            simplicity in the xA's overall appearance to allow it to possess 
            much dignity. Six exterior colours are available: White pearl, 
            silver metallic, Bordeaux red, yellow pearl, dark blue metallic, and 
            black.<br>
            <br>
            <b>Indoors</b><br>
            The xA seats four adults in comfort with room to easily fit four 
            large boxes containing a dozen 1.5-litre bottles of Masafi in the 
            luggage compartment. Inside, it offers modern, industrial styling 
            with metal-tone instrument panel trim as well as elegant 
            black-on-white gauges including a distinctive and bold speedometer 
            and tachometer positioned in the centre of the dashboard. There are 
            plenty of storage holders which includes a smart illuminated 
            see-through storage compartment. The high centre positions of the 
            meters help visibility and legibility.<br>
            The xA is fitted with power steering, windows, door locks and 
            mirrors. It also has a very effective air conditioning unit, rear 
            window defogger and rear window wiper. Plus, fine touches like 
            dimpling on the surface of the door trims and consoles give a 
            distinct impression that this hatchback is a cut above its rivals. 
            However, the car's radio/cassette 4-speaker audio system (fitted as 
            standard) may be in need of an upgrade to accept CDs, or even MP3s, 
            to entice its 'hip-hop' clientele.<br>
            <br>
            <b>Performance</b><br>
            The xA offers a lively performance for a hatchback. The 1.5-litre 
            16-valve inline engine produces a nimble 107 HP at 6,000rpm and a 
            maximum torque of 142Nm at 4,200rpm. The engine is equipped with 
            advanced VVT-I (Variable Valve Timing-Intelligent) technology and a 
            performance-tuned suspension. You can either choose the 5-speed 
            manual or a 4-speed automatic Super ECT (Electronic Controlled 
            Transmission) which is designed to give better fuel efficiency (data 
            for which was unavailable) and smoother shifting through gears. OER 
            test-drove the automatic that can accelerate from nought to 100km/h 
            in 12.2 seconds. You can shave two seconds off this velocity rate by 
            choosing the manual transmission but, as noted, both have fluid 
            handling and respond energetically. <br>
            Another major selling point of the xA, though, is its excellent 
            cabin quietness. Toyota's chief engineer on the car explained that 
            this is due to a curved panel in the cabin's floor and effective 
            deployment of acoustic and insulating materials. Additionally, dash 
            and windshield vibration as well as engine resonance are also kept 
            to a minimum while insulation under the trim, garnish and carpets 
            absorb sound further still.<br>
            <br>
            <b>Safety and security</b><br>
            The xA is equipped with ABS (Anti-locking Brakes System) with EBD 
            (Electronic Brake force Distribution), and Brake Assist assuring 
            enhanced vehicle stability during emergency braking manoeuvres. Air 
            bags for driver and passenger are also fitted as standard. Seats use 
            WIL (Whiplash Injury Lessening) technology designed to reduce stress 
            on occupants' necks during low-speed impacts from the rear while the 
            seatbelts possess pre-tensioners and force limiters. The fact that 
            the meter panel is centrally located can also be considered as a 
            safety measure as it reduces the need to flit eye contact between 
            instrument panels and the road ahead.<br>
            <br>
            <b>The finishing line</b><br>
            The xA is a 'one of a kind'. It is an eye-catching hatchback that is 
            simple yet stylish and offers premium design and styling for an 
            affordable price. It will undoubtedly be a success and it is 
            definitely worth a closer look. <br>
