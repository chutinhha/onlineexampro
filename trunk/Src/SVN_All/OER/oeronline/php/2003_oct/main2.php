            increases by between 8 - 12 % in the past five years and the number 
            of cars in use in Oman has risen to about 280,000 passenger cars and 
            170,000 commercial vehicles. Moreover, the car market high growth 
            potential shows little sign of slowing down.<br>
            In 1990, Omanexpo organised the Sultanate's first-ever 'Motor Show' 
            drew thousands of visitors and provided clients with the perfect 
            opportunity to preview new products, evaluate market trends and 
            establish long-lasting and commercially profitable partnerships. 
            Last year, over 25,500 visitors were recorded attending the event. 
            High profile visitors to the Motor Show 2002 included automotive 
            majors from across the world, their local representatives, car 
            designers, automotive engineers, finance companies, auto insurance 
            companies, fuel part suppliers, lubricant companies, pre-owned car 
            companies, wholesalers, retailers, senior government officials, the 
            press as well as the general public. As the event picks up extensive 
            coverage from both the Gulf and domestic press, the company predicts 
            much higher visitor numbers for the motor show this year. &quot;We can 
            expect an attendance of over 30,000 at Motor Show 2003,&quot; says CJ 
            Paul, General Manager of Omanexpo.<br>
            So what are the major attractions of Motor Show 2003 to car dealers, 
            car accessory companies and the general public?<br>
            Well, the major draw for exhibitors (essentially automobile 
            dealers), according to Omanexpo, is the fact that they can showcase 
            new automobiles to the general public. Although, perhaps more 
            importantly in the domestic market, the event serves as a platform 
            to build up sales interest ahead of the holy month of Ramadan when 
            automobile sales are at their peak. Automobile dealers continue to 
            view this show as a great opportunity in the Sultanate to gauge the 
            public reaction towards their latest launches at this crucial time 
            of the year when car sales peak. Omanexpo explain that the event may 
            also improve brand equity and generate greater awareness among the 
            general public about particular models.<br>
