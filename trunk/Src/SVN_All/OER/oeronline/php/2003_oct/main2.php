            Exhibition Centre, will, inevitably, be a great opportunity for car 
            enthusiasts, potential buyers, dealerships, accessory suppliers and 
            the rest of the auto market fraternity to get together and talk shop 
            ahead of Ramadan - a holy month when Oman's auto retailers offer 
            significant discounts and deals to customers. <br>
            Oman's car market is booming, according to insiders although there 
            are no official figures to verify these sentiments. The Sultanate's 
            automotive retail sector has been one of the fastest growing sectors 
