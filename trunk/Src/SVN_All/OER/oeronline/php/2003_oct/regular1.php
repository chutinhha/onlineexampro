            <font face="Verdana" size="4" color="#FF0000">Collaboration pays<br>
            </font><font face="Verdana">
            <br>
            </font><font face="Verdana" size="1"><b>Those Omani companies that 
            give greater attention to Collaborative Possibilities will have an 
            edge over their counterparts that do not, writes K S Kumar</b><br>
            <br>
            A couple of issues ago, I'd written that Omani businesses faced 
            unique challenges due to increased, and qualitatively different, 
            competition from large regional players who can leverage the 
            advantage of a large domestic and/or re-export market. In free 
            markets, when competition intensifies beyond a point, typically, 
            companies try to improve their profitability through mergers and 
            acquisitions (M&amp;As), and this has happened in Oman too. <br>
            However, M&amp;A in Oman still has to fully overcome cultural obstacles 
            such as perceived failure and loss of face, particularly when large 
            business groups are involved. An option I had therefore proposed was 
            to actively examine possibilities of collaboration. Since then, I've 
            received a lot of feedback, some asking for the ideas that I had 
            committed to share and some openly sceptical that such an approach 
            could work in Oman. I thought it best, therefore, to explore a few 
            examples in different sectors which can answer many of the questions 
            raised.<br>
            <br>
            <b>Approach</b><br>
            In devising a company's approach to Collaborative Possibilities (CPs), 
            focusing on the quantifiable benefits of CPs will lead to quick 
            understanding. An initiative that will lead to reduction in costs or 
            increased revenue - the potential benefits is clear and results 
            measurable and for this reason, most CPs target such benefits.<br>
            As some of the examples below show, CPs need not be between 
            companies in the same sector; they need not even be between 
            companies in the same country. Successful collaboration can happen 
            between a limited number (even 2) of players and large participation 
            is not a prerequisite.<br>
            What is important, however, is that the companies must be positive 
            and you are definitely better off without a half-committed and 
            doubtful player who will then certainly not stay the course. The 
            chances of implementing a CP are high if there is a more or less 
            equal balance of power between the companies and chemistry between 
            the CEOs.</font></p>
                    <p>
            <font face="Verdana" size="1"><b>Industry</b><br>
            Let us consider the non-oil manufacturing companies in Oman. Some 
            CPs are:<br>
            a) As we know, exports are a necessity for most industrial units in 
            Oman to make profits. But success is difficult due to competition, 
            costs of entry and the learning curve involved. So, what can we do? 
            Units that manufacture non-competitive products - for example, 
            biscuits, batteries, auto-filters and so on - can share 
            infrastructure costs such as office space, administration backup, 
            warehousing and logistics in export markets. (The sharing of 
            logistics and other administration costs can obviously also be 
            evaluated for the Omani market itself.) Such an approach will cut 
            costs, share the risks and enable wider market coverage.<br>
            b) Companies may have successful track records in different export 
            markets and, if so, could share the consumer and trade insights with 
            each other that could make a dramatic difference to success or 
            failure. Understanding those insights is the key to successful entry 
            into different countries, and a lack of it could cause failure. 
            Large MNCs too, often, fail to think and act local leading to losses 
            and eventual exit.<br>
            c) Market research is another area of CP. Quality research is 
            expensive and that leads to companies either doing limited or nil 
            research. However, by collaborating, companies can make better 
            data-based decisions.<br>
            There are other areas too that can be examined such as procurement, 
            recruitment, production support facilities and so on. In the final 
            analysis, the choice of a particular area will depend on how key the 
            proposed area is to the companies concerned.<br>
            <br>
            <b>Tourism</b><br>
            Another important sector in Oman is tourism, and there is a gap 
            between what is possible and the actual status. I'd explored some of 
            them through the case study outlined in the August issue. Some 
            examples are:<br>
            a) Tourism companies can seek to exploit the potential for inbound 
            tourists from GCC countries by collaborating with established travel 
            agencies in these countries. Representatives can be posted at the 
            travel agencies' offices in these countries prior to the peak season 
            and the collaborator paid a commission on the business generated.
            <br>
            b) A tourism company (or companies) can seek to collaborate with 
            hotel chains such as Inter Continental, Sheraton, Hyatt and so on 
            that have properties in both Oman and Dubai. A possibility is to 
            have a manned counter at the Dubai property with an arrangement to 
            pay a commission on the revenue generated, if necessary with a 
            guaranteed minimum. Naturally, the Oman hotel would benefit by 
            increased occupancy and could, therefore, facilitate this 
            arrangement.<br>
            There are other possibilities too and again, the above points are 
            only illustrative.<br>
            <br>
            <b>Retailing</b><br>
            CPs can be identified in other sectors too. Let us look at a sector 
            like retailing, the focus of the OER Cover Story this issue, where 
            large players like Carrefour and LuLu have now become quite 
            aggressive in Muscat and Salalah. While Carrefour establishes malls 
            with its hypermarket at the centre, Lulu has followed a large 
            departmental store format with attractive prices being its main 
            draw. So what will be the successful model in future for retailing 
            in Oman? <br>
            The future will be a function of many variables such as purchasing 
            power, population and traffic density, buying habits (convenience vs 
            price) and so on. The chances are both types will coexist and cater 
            to different customer segments, although given the Omani customer's 
            price sensitivity, the attractively priced departmental store will 
            have a larger market share. So what options do existing local malls 
            like Al Araimi and Sabco have? Some thoughts are:<br>
            a) Malls can collaborate rather than compete on promotions thus 
            ensuring Ômega promotions' and more attractive prizes.<br>
            b) Local malls have the key advantage of local knowledge and they 
            can use this to develop an economically feasible model (department 
            store?) for the interior markets. This can be attempted through a 
            Joint Venture (JV), and if it succeeds, then it can lead to a merger 
            in Muscat itself. Again, the above is illustrative only.<br>
            <br>
            <b>Implementing a CP</b><br>
            Many good strategies have failed due to bad execution and what makes 
            companies great and long lasting is their ability to consistently 
            execute well. In meetings and seminars, I have always maintained 
            that the vision bit is the easy part; doing it well over a period of 
            time is tough. So too it is with CPs.<br>
            For a CP to succeed, clear objectives and explicit understanding 
            between the companies is a must. Execution will be challenging, but 
            some guidelines could be:</font></p>
                    <ul>
                      <li><font face="Verdana" size="1">The area(s) of 
                      collaboration must be explicit and documented. </font>
                      </li>
                      <li><font face="Verdana" size="1">The CP should be 
                      formally structured with responsibility and authority 
                      being clear to all concerned.</font></li>
                      <li><font face="Verdana" size="1">Costs and expected 
                      benefits must be defined.</font></li>
                      <li><font face="Verdana" size="1">It must be given enough 
                      time to succeed and that is best achieved if a minimum 
                      period is specified.</font></li>
                      <li><font face="Verdana" size="1">Clear milestones and MIS 
                      are necessary for ongoing transparent assessment.</font></li>
            </ul>
            <p>
            <font face="Verdana" size="1">Collaborations are more likely to work 
            between companies in the same business that are reasonably balanced 
            in terms of power. The other critical soft issue is that of the 
            relationship between the companies involved. To the extent that 
            there is an existing and cordial business relationship between the 
            shareholders and/or the CEOs, the chances of success are greater. 
            While the benefits sought from a CP are rational, like in all human 
            interactions, there is a lot of comfort in dealing with known and 
            trusted people. Even if there is no prior relationship, there must 
            at least be chemistry between the CEOs concerned so that they will 
            be supportive of the effort, particularly in difficult times.<br>
            <br>
            <b>Conclusion</b><br>
            The idea of collaboration is hardly new -when faced with a common 
            difficulty, businesses have always come together to seek solutions. 
            This is what I refer to as reactive collaboration.<br>
            But the business landscape in Oman is changing rapidly. There are 
            many pressures on the private sector. They must remain profitable in 
            increasingly difficult times while competing without protection 
            against large regional players; they also have a responsibility to 
            provide employment to Omani youth. Meeting all these objectives 
            certainly requires us to look at things differently. We can 
            therefore certainly apply collaboration in a proactive way to take 
            advantage of opportunities that we may otherwise miss.<br>
            <br>
            <i>K.S. Kumar has been a practising senior manager in Oman for over 
            20 years in a diverse range of business</i></font></p>
