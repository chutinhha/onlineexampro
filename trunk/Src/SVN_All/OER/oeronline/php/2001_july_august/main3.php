                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">What's up with Oman's Banks?</font><p><font face="Verdana" size="1"><b>Omani banks are making headlines for all the wrong reasons.&nbsp;<br>
                    What is going on? OER takes a closer look&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/bank1.jpg" align="left" hspace="5" vspace="5" width="225" height="182">If you read the newspaper headlines, Omani banks would appear to be in dire straits. As if slumping profits weren't enough, their exposure to bad loans is undermining investor confidence, pushing their share prices into the mire.&nbsp;<br>
                    The chairmen of two big banks have stepped down in recent months.&nbsp;<br>
                    In May, a newspaper went so far as to question whether one major bank had complied with international accounting standards, claiming it should have posted a net loss in the first quarter instead of a net profit. The bank categorically denied the claim.&nbsp;<br>
                    Not surprisingly, the investing community, which considered banks a safe haven after the share market slump, is on edge.&nbsp;<br>
                    What is going on?<br>
                    Industry analysts say Oman's banks are feeling the effects of some bad decisions in the early 1990s, when they loaned money too freely. Some of the companies they lent money to have since gone bust, or are not profitable enough to pay back the loans.&nbsp;<br>
                    The collapse of the Muscat Securities Market in 1998 also ruined many companies and bankrupted individual businessmen who had borrowed to speculate on shares.<br>
                    Now banks are making massive provisions for bad loans. Even so, some analysts are questioning whether they are setting aside enough.<br>
                    NBO Chief Executive Aubyn Hill says the best approach is to make "as much provision as is required as quickly as possible".<br>
                    "Investors and analysts will soon reward those institutions which adopt this approach," he says.&nbsp;<br>
                    "Those that do will begin to give substantial rewards to shareholders and investors in the next two to three years."<br>
                    But Hill agrees that the poor performance of Oman's corporate sector has hit banks in the back pocket, and it will take time for them to recover.<br>
                    "Slow economic growth for the past three years has affected the businesses and subsequently their bankers," he says.<br>
                    "Both manufacturing and trading activities are not doing well and that is getting reflected in the loan books of many banks.&nbsp;<br>
                    "Many companies are still facing slack demand and borrowing less and, therefore, banks will find it difficult to generate the kind of returns they made in the late 1990s."<br>
                    At the same time, Hill says the banking industry is grappling with a period of transition.&nbsp;<br>
                    "The whole sector is moving away from a period of just adequate transparency to an era of high transparency," he says.&nbsp;<br>
                    "Therefore, there is a lot of room for interpretation and even confusion, especially among analysts and the media."<br>
                    At the centre of this confusion is International Accounting Standard 39 (IAS39) - a new rule introduced this year that requires all institutions to use a "net present value" approach in deciding the current value of their loans and other assets.<br>
                    In other words, banks now have to take into account the present value of future cashflow, and adjust their books accordingly. Essentially, IAS39 accounts for the erosion in the value of money over time.&nbsp;<br>
                    "Due to this stringent provisioning norm, the balance sheet will reflect the real financial position of the bank," one analyst says.<br>
                    The Central Bank of Oman (CBO) has also put limits on how much banks can lend to individuals, restricting lucrative personal loans to 35 per cent of a bank's total lending profile.&nbsp;<br>
                    Bankers agree that the new norms and accounting standards will make for a stronger banking sector in the long run, but say it is taking the industry time to adjust.</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/bank2.jpg" align="right" width="225" height="211">"It will be a difficult period for the banking industry," says Hill.<br>
                    All major commercial banks, including Bank Muscat, Oman International Bank (OIB) and NBO, are reporting sharp drops in their net earnings.&nbsp;<br>
                    According to the CBO, last year Omani banks saw their combined net profits drop 5.3 per cent. The slide has continued in the first quarter of this year, with the Sultanate's banking sector recording a 15 per cent drop in net profit to RO20.6 million in the three months to March.&nbsp;<br>
                    This is despite growth in the combined assets of the banking sector, which rose to RO4.019 billion by the end of March - 10.4 per cent higher than a year earlier.&nbsp;<br>
                    Abdul Kader Askalan, Managing Director of Oman Arab Bank, says things are not about to improve quickly.&nbsp;<br>
                    "I don't think this year will be better than last year," he says.&nbsp;<br>
                    "The net earnings of banks during the first quarter this year were much less than the corresponding period last year."<br>
                    The bad loan headache is forcing Omani banks to adopt a much more conservative lending stance. Banks are also turning down requests for fresh overseas loans after the default by a GCC client of one major bank recently.&nbsp;<br>
                    Hill says NBO is moving away from corporate lending in overseas markets to reduce its risk profile.<br>
                    The bank's strategy, he says, is to grow corporate lending "very selectively in Oman, and to move out of corporate lending in our overseas branch countries (United Arab Emirates and Egypt)".&nbsp;<br>
                    "This means that our aim is to build our retail assets and fee-based income in foreign countries," he says.&nbsp;<br>
                    Analysts say the CBO's moves to increase transparency and tidy up the banking sector should see Oman's banks move into stronger territory in 2002.&nbsp;<br>
                    But there are fresh challenges ahead.&nbsp;<br>
                    Oman's entry into the World Trade Organisation requires the Sultanate to open its retail banking sector to international banks within two years, posing a potential threat to local players.<br>
                    Some banking analysts doubt it will be an issue.<br>
                    "Ours is a small market," says one. "If a foreign player really wants to enter the retail banking business, he has to invest a substantial amount and the returns will come at a later stage. Therefore, it will be difficult for a foreign bank to get established in the local market."&nbsp;<br>
                    As far as corporate business is concerned, another analyst points out that foreign banks are already funding major projects, "so what difference will it make if Oman opens up its financial sector?"<br>
                    Hill, however, feels that foreign and regional banks will enter the local market, and he believes Omani banks should prepare for their arrival.&nbsp;<br>
                    "Local banks should seek to gain more experience in overseas markets in order to find additional revenue and diversify risk," he says.<br>
                    If Omani banks have learned one thing in recent years, it is the need to prepare for change.</font></p>

