                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Hey Big Spender</font><p><font face="Verdana" size="1"><b>Living on credit is becoming the norm in Oman, and for many people, debt is already a way of life. Are we living beyond our means? The CBO's F Al Mandhary reports</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    <img border="0" src="../../images/2001_july_august/sim1.jpg" align="left" hspace="5" vspace="5" width="225" height="161">There are those who blame the commercial banks for laying loans at people's feet and encouraging the consumerism that has spread through society. Others argue that consumerism is healthy for the economy, and that people are only lifting their standard of living to keep pace with the rest of the world. Still others claim that we a living beyond our means and refusing to accept a standard of living that suits our income levels.<br>
                    Whatever the case, it is largely accepted that many are choking on the burden of huge loans which, despite regular repayments, never seem to disappear. On the contrary, the debts seem to be getting larger.<br>
                    <br>
                    </font><b><font face="Verdana" size="1">SOCIAL OBLIGATIONS AND COMMITMENTS</font></b><font face="Verdana" size="2">

                    <br>
                    Taqi Ali Sultan, deputy general manager at the National Bank of Oman (NBO), says Oman's rising tide of personal debt is not a general trend of overspending.&nbsp;<br>
                    Rather, it is a case of Omanis providing for their extended families in a more consumer-orientated society.<br>
                    "You have to look at the structure of the family and the social needs and obligations here," he says. "Families are large, and there's only one person supporting the family. On the face of it, that person is overspending well beyond their needs. But if you go deeper into his problems you will find that he's supporting a family of eight or nine and he's the only one earning a salary."<br>
                    Abdul Aziz Mohd Al Balushi, also a deputy general manager at NBO, agrees. He says: "As the family extends and obligations increase, the income remains constant. With a high population growth, the expenses increase even though you are not spending on yourself but on the family.&nbsp;<br>
                    "Our social obligations are not the same as in Europe. The extended family is an obligation."<br>
                    Oman has a young, rapidly growing population, with 57.7 per cent under the age of 20 and 37.5 per cent between the working ages of 20 and 59. Oman's population is estimated to be growing at an annual rate of 3.5 per cent.&nbsp;<br>
                    Typical of such a young society, the culture tends to be one of consumerism rather than saving. As these young people set out in life, it is natural that their initial spending should exceed their earnings.&nbsp;<br>
                    Buying a car is often the crucial first move given the hot weather, limited public transport and the long distances people need to travel to work.<br>
                    Marriage costs are also significant, as the man spends thousands on a dowry and marriage preparations.<br>
                    A tendency to move out of the extended family and acquire separate housing, be it rented or purchased, soon adds up, as do additional furnishing and household appliance costs.<br>
                    The fast-expanding family increasingly adds its share of the financial burden.<br>
                    Despite the growing trend of separate housing for newly married couples, working family members continue to remain socially and financially responsible for the rest of the extended family.<br>
                    Prime facie, it would appear that the 37.5 per cent of the population of working age is supporting 62 per cent of the Omani population.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>COMMERCIAL BANK LENDING</b></font><font face="Verdana" size="2">

                    <br>
                    Needless to say, spending quickly exceeds earnings and many turn to banks for loans.&nbsp;<br>
                    Statistics on commercial bank lending support this theory and show a steady growth of credit to the public for several years.<br>
                    Personal loans totalled RO184.3 million in 1990. By 1995, the number had more than doubled to RO442.4 million.&nbsp;<br>
                    In 1996 personal loans reached a total of RO552.8 million. In 1997, sparked by the soaring stock market and the rush of the public to invest, the total jumped 45 per cent to RO803.2 million.&nbsp;<br>
                    Noting the continuing growth of personal loans, in January 1999 the Central Bank of Oman (CBO) told banks they had to reduce personal loans to 30 per cent of their total lending.&nbsp;<br>
                    Under this regulation, personal loans were defined as "those granted to individuals and households for non-business purposes".&nbsp;<br>
                    It was hoped that cutting down consumer loans would encourage citizens to curb their spending and contribute to the development of the national economy.<br>
                    This regulation slowed the rate of increase in personal loan lending. Total personal loans in 1998 stood at RO948.7 million, up 18 per cent from the previous year. In 1999 the rate of growth slowed to 6.0 per cent, with the total rising to RO1006.3 million. In 2000, total personal loans fell for the first time to RO978.2 million, down 2.7 per cent from the previous year.<br>
                    Of the 2,980,733 accounts that took credit with commercial banks last year, 90.2 per cent were registered as personal loans compared to 91.6 per cent in 1999.<br>
                    In August 2000 the CBO, responding to requests by both commercial banks (whose profits were hit hard by the measure) and the public, decided to raise the ceiling of personal loans to 35 per cent of a bank's total lending.<br>
                    Nonetheless, commercial banks were reminded of their obligations towards their customers and advised to ensure that borrowers were able to fulfill borrowing conditions and set sound platforms for savings to meet future economic and social needs.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>POOR SAVING HABITS AND LACK OF LONG TERM
                    FINANCIAL PLANNING</b></font><font face="Verdana" size="2">

                    <br>
                    As a nation, Oman's saving and investment to GDP ratios are lower than those of neighbouring GCC nations. According to IMF 1991-97 figures, Oman's domestic saving to GDP ratio, at 24.8 per cent, was lower than the GCC average ratio of 28.2 per cent.<br>
                    The investment to GDP ratio, at 16.4 per cent, was six points lower than the GCC average.<br>
                    While there is no denying the high social obligations many Omanis face, what is clear to many bank officials is that the public, to a large extent, has no visible long-term financial plan, and saving habits are very poor.<br>
                    Says one bank official: "There is no saving culture in Oman. Absolutely none."<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>BANKS AS CATALYSTS PROVIDING EASY MONEY</b></font><font face="Verdana" size="2">

                    <br>
                    Loans are very popular in Omani society, and banks are keen to lend.<br>
                    People shop around for the largest amounts they can borrow, and banks compete to grab these customers by offering large amounts for long terms.<br>
                    Some see the commercial banks as the root of the problem.<br>
                    As the Arab saying suggests: "Even the Qadhi (judge) will drink from the free wine".<br>
                    Given in multiples of salary, consumer loans range from 25 times the salary to as high as 45 times the salary. Pay back periods range from four to eight years.&nbsp;<br>
                    While many banks try to counsel their customers on the huge liability they are imposing on themselves, the basic criteria for giving someone a loan is their earning capacity.<br>
                    Critics claim the banks know some customers will not be able to meet their obligations after loan installments, yet grant the credit so long as the borrower's salary is coming in.<br>
                    But while admitting that a problem of indebtedness is emerging in Omani society, most in the banking community are quick to deny their role as a catalyst.<br>
                    Rather, they see their role as a business, selling goods to customers.<br>
                    How, they ask, can a shop owner go around assessing the shoppers and questioning them about whether they actually need the goods and whether they can afford a particular item?<br>
                    Others disagree, calling for banks to be more cautious about the amounts they hand out to their customers.<br>
                    "The debit to balance ratio should be limited to 40 to 50 per cent of the net salary," says Saleh Mohammed Al Shaksy, customer services manager at HSBC.&nbsp;<br>
                    "A person earning RO200 should have a smaller limit of burden to ensure that whatever he is left with at the end of the month is sufficient to cover his basics.<br>
                    "Many banks are presently leaving customers with as little as 35 per cent of their income. I know people who are left with RO35 a month."&nbsp;<br>
                    Whoever is to blame, banks do concede that Omanis tend to borrow a lot.&nbsp;<br>
                    "By any standards, the kind of loans given in this country are in fact quite large," says NBO assistant general manager and chief credit officer Mr R. Ramaswamy.<br>
                    "Omanis tend to avail themselves of whatever facility is available at the bank and very rarely do they make an assessment of their repayment capacity."<br>
                    It is generally agreed that lower income people suffer more from the burden of high indebtedness.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>LOWER INCOME GROUP OVERBURDENED BY LOANS</b></font><font face="Verdana" size="2">

                    <br>
                    Majan International Bank general manager Salaam Said Al Shaksy says it is the people who earn less than RO500 a month who are most at risk if not monitored properly.<br>
                    "If we don't take the right kind of action to reduce the kind of debt burden that people are experiencing, we may face a problem with them finding it difficult to meet their monthly commitments," he says.<br>
                    "Many are very stretched and I believe that one of the reasons is that banks have been slow in differentiating between the higher income and lower income groups. If I apply the same benchmark for a person with a salary of RO250 as I do to a person with a salary of RO1000, then I am making a big mistake."<br>
                    Statistics from the Ministry of the National Economy show a significant portion of the workforce falls into the low income group. The 1999 average salary of Omanis working in the private sector and registered with the Public Authority for Social Insurance was RO170 a month.<br>
                    Of the 50,666 Omanis working in the private sector, 80 per cent are earning below RO200, while 16.3 per cent are earning between RO200 and RO500. Only 3.5 per cent earn above RO500 a month.<br>
                    Credit managers in many of the banks are able to quote several cases of low-income customers who face problems making ends meet and who seem to have no idea how to get out of their predicament.<br>
                    "There is a problem in that these loans keep dragging on and on," says BankMuscat deputy general manager of retail banking, Saad Musa Al Jenaibi.<br>
                    "It seems like there are ongoing requests for top ups on loans and waivers, and people do not seem to have a loan plan. They don't take a loan for a specific purpose or for a specific period. They take a loan of 10 thousand, pay two thousand, and come up for a request for the paid amount as a top up."<br>
                    However, the problem is not by any means limited to the lower income group. As many officials will admit, there are those in the higher income group earning as much as RO2000 a month who are left with less than RO200 at the end of the month to meet family commitments.<br>
                    Loan top ups and regular waiver requests are just as popular in this group as in the rest of the population.<br>
                    In fact, credit officials in commercial banks will admit that once the customers take the loan, top ups are inevitable.<br>
                    "People who start taking loans will always come back for top ups," says HSBC's Saleh Mohammed Al Shaksy.<br>
                    "Always. It's unbelievable.<br>
                    "We've just changed our policy and increased our period for top ups from six months to 12 months, and people wait for that day to come."<br>
                    It should be noted, however, that Omanis are generally good at paying their loan installments. CBO figures for 2000 show that of the RO978.2 million in total commercial bank personal loans and advances, less than five per cent was considered "non-performing" Ñ that is, loans where three or more installments have not been met.<br>
                    <br>
                    </font><b><font face="Verdana" size="1">LOOKING FOR SOLUTIONS</font></b><font face="Verdana" size="2">

                    <br>
                    In a free-market economy, it is difficult to tell people not to spend. But if something is not done, Omanis will soon start to feel the pinch if they haven't already.<br>
                    The time will come when people will no longer be able to pay back their loans. Already many customers face problems when they retire and their income is reduced, putting a huge strain on their pay-back capacity.<br>
                    Even as we speak of the Omani culture and burden of the extended family, it is important to realise that culture is learned and not innate.<br>
                    What we see as part of today's cultural trends did not exist 20 or 30 years ago.&nbsp;<br>
                    What is suggested here is not a change in our values but a change in our habits and thinking.<br>
                    To suggest we live within our means is not an insult to the way we are, but a call to think beyond today's needs.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>CALL FOR NATIONAL EDUCATION CAMPAIGN ON SAVING</b></font><font face="Verdana" size="2">

                    <br>
                    Most of the banks spoken to suggested that a national education campaign was necessary to create and inculcate a deep awareness of the need to save, the need to cut down on spending, and the importance of financial planning.&nbsp;<br>
                    "The Omani population needs to take a real look at its needs," says Mr Ramaswarmy.<br>
                    "What is necessary, and what is a luxury that can be trimmed out?<br>
                    "There is always a virtue in savings money, there's always a virtue in spending less, there is always a virtue in not borrowing. These are the three virtues which parents can inculcate in their children as a parental responsibility. No bank can do it for us."<br>
                    Majan International Bank's Salaam Al Shaksy agrees: "When you talk of getting out of this problem you can't just look at the banks and ask them to stop lending. There has to be a cohesive, integrated, committed national policy on an issue.&nbsp;<br>
                    "You must inculcate a savings culture and this must be done at an early age. You cannot spend like there's no tomorrow."<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>CALL FOR MORE TRANSPARENCY</b></font><font face="Verdana" size="2">

                    <br>
                    Others point to a need for changes in the CBO regulations. Several officials said that banks were presently limited in their capacity to assess customers' financial standing. They called for greater transparency in credit checks.<br>
                    At present, when assessing a customer's financial status for commercial banks, the CBO's credit check does not include other financing companies in the country.<br>
                    It was suggested that the Central Bank include all forms of financing in its reporting.<br>
                    <br>
                    F Al Mandhary is an editor on the CBO's Al Markazi publication.</font></p>

