                    <p><font face="Verdana" size="2">Even so, by Gulf standards Oman remains an oil-producing minnow. Its reserves pale in comparison to those of neighbouring Saudi Arabia, which sits on 259 billion barrels of proven oil reserves - more than a quarter of the world total - and up to one trillion barrels of ultimately recoverable oil.&nbsp;<br>

                    Where Saudi produces more than nine million barrels of oil a day, Oman produces less than one million.<br>

                    The Sultanate knows that even if its oil lasts a lot longer than originally thought, it will eventually have to find other sources of income. For this reason, it has embarked on a progressive programme to diversify its economy away from oil. And central to that diversification programme is gas.<br>

                    "We keep on being reminded that we only have 17 years before we run out of oil," Rumhy says with a wry smile, "so gas is really going to play a big role here. If we are to go industrial in this country, we need power. Gas will give us that option."<br>

                    More and more natural gas is being discovered in Oman, and the Sultanate's proven gas reserves of 22 trillion cubic feet are expected to continue to swell in coming years.&nbsp;<br>

                    This year Petroleum Development Oman (PDO), which explores for natural gas on behalf of the Government, announced the discovery of what is potentially the largest gas field found in Oman in the last six years.<br>

                    <br>

                    Gas is increasingly being used to develop new industries and create much-needed jobs, for example by establishing fertiliser, methanol and petrochemical plants.<br>

                    And Rumhy says two major gas pipelines currently under construction will speed the transformation of the Omani economy when they open in late 2002.&nbsp;<br>

                    The pipes will link Oman's gas fields to the southern port city Salalah and to Sohar in the north, effectively covering the entire Sultanate.<br>

                    "This will break the chicken and egg story," Rumhy says.&nbsp;<br>

                    "In the past, if people wanted to set up an industry they asked for gas. And when the Government looked at new pipeline projects, the first thing it asked was where is the industry?'. "Fortunately we have broken this cycle of thinking, and I am sure once investors see the availability of gas anywhere in Oman, they will want to tap in."<br>

                    <br>

                    <img border="0" src="../../images/2001_july_august/im3.jpg" align="left" width="225" height="234">Gas is also becoming an increasingly important export commodity for Oman.&nbsp;<br>

                    The Sultanate began supplying Liquefied Natural Gas (LNG) to customers in Korea and Japan early last year. Rumhy says that in an increasingly environmentally-conscious world, sales of clean-burning LNG have enormous growth potential and could account for as much as 10 per cent of Government revenues within a decade.<br>

                    "I think gas is the energy of the future," he says.&nbsp;<br>

                    "Until we manage to come up with a technology that makes coal clean, people will think gas. The Chinese are thinking gas, and so are the Indians, so that's a potential market for us, and there are others as well."<br>

                    Oman is looking to emerging markets in Asia such as Myanmar, Vietnam, Laos and Cambodia as potential gas customers, he says.<br>

                    "These are very entrepreneurial people, and they work very hard. As soon as they sort out a few of their internal problems, we will see incredible growth coming from those countries."</font></p>

                    <p><font face="Verdana" size="2">The Government is close to proceeding with a third gas liquefaction train at Oman LNG's plant in Qalhat - already the largest operative gas liquefying plant in the world.<br>

                    <img border="0" src="../../images/2001_july_august/im4.jpg" align="right" hspace="5" vspace="5" width="225" height="231">Rumhy says the third train, which will cost close to US$1 billion to build, will proceed as soon as buyers for the gas are secured, which he hopes will be before the end of the year.<br>

                    "There are small delays because with gas, unlike oil, you first sell and then you develop," he explains. "We are at that stage of selling. Until we secure the market, we cannot physically start the project.<br>

                    "But a lot of work has been done.&nbsp;<br>

                    "The engineering behind the third train is ongoing, it's almost finished. So hopefully towards the end of this year we will make a decision based on the marketing issues. If marketing is successful, by the end of this year we should start serious work."</font></p>

