                          <td width="100%"><font face="MS Sans Serif" size="2" color="#006FA4">Powerful computers and software are allowing experts to better

                    "see&quot; what is underground. Techniques such as "integrated subsurface modelling&quot;, which combines detailed field geology models with simulated oil, gas and water flow, have enabled Petroleum Development Oman to add hundreds of millions of barrels of oil to the country's reserves. They have also resulted in accelerated production, and prevented the drilling of unnecessary wells.<br>

                    The ability to maximise production from existing fields is as important as finding and developing new ones. The Yibal field - Oman's largest - is a good example of how the application of new technology can breathe new life into a field whose production was expected to decline. More than 30 years after Yibal came on stream, it still accounts for about a fifth of PDO's total production. In the 1990s horizontal wells halved the field's per-barrel production cost. More recently, multi-lateral wells, which have multiple offshoots emanating from a single main well, have halved the cost again.<br>

                    The Internet is also opening up new possibilities in oil and gas exploration, allowing engineers around the world to communicate and share information instantaneously. The development of the recently-discovered Al Noor field in Oman is a good example. The Al Noor reservoir rock is extremely compact, presenting particular problems for production. A worldwide virtual team worked to unlock the complex geology of the rock, which can be visualised on computers in three dimensions. Techniques such as seismic imagining and massive fracturing of the rock ultimately made it possible to produce oil and gas economically from the field.</font></td>

