                    <font face="Verdana" size="4" color="#FF0000">In the Black</font>

                    <p><font face="Verdana" size="1"><b>Much has been said about Oman's modest oil reserves, but will the Sultanate really run out of black gold in 17 years? OER Editor Matthew Brockett talks to the man who should know</b></font></p>

                    <p><font face="Verdana" size="2">Oman doesn't have a lot of oil by Middle East standards. With only five billion barrels of proven oil reserves, it is estimated that the Sultanate's wells will run dry in 17 years.&nbsp;<br>

                    Or is it?<br>

                    "It's not as simple as that," says His Excellency Dr Mohammed bin Hamad Al Rumhy, Oman's Minister for Oil and Gas.<br>

                    "The estimate is a statistical figure based on what the industry calls a recovery factor, and the average recovery factor in Oman is very small."<br>

                    But new technologies, he says, are improving recovery techniques all the time.&nbsp;<br>

                    In an exclusive interview with OER, His Excellency reveals that Oman now expects to extract a lot more oil than previously thought.<br>

                    <br>

                    The Sultanate sits on nearly 50 billion barrels of oil, but the majority of it is unlikely to see the light of day.<br>

                    "This oil is not a pool," Rumhy explains. "It sits in rocks and you can never get all of it out. It's impossible. It's like spilling milk on a piece of rock and trying to retrieve it."<br>

                    Since Oman discovered oil in 1962, 6.1 billion barrels have been extracted, or just over 12 per cent of the total resource base (see graph).&nbsp;<br>

                    Until now, that Sultanate has estimated it can extract a further 10 per cent, or five billion barrels.<br>

                    "This is what we've been telling people are our reserves," Rumhy says.&nbsp;<br>

                    "But using today's technology, we are confident that we can increase this by another four billion barrels, or another eight per cent."<br>

                    That means that if production rates remain the same, Oman will be able to produce oil for almost twice as long as previously estimated - no small matter when you consider oil accounts for 80 per cent of export revenue and about 40 per cent of gross domestic product.&nbsp;</font></p>

                    <p align="center"><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/im2.jpg" width="300" height="226"></font></p>

                    <p><font face="Verdana" size="2">Rumhy briefed His Majesty Sultan Qaboos bin Said on the encouraging news in June. And he says the four billion barrel increase in recoverable reserves is just the beginning.&nbsp;<br>

                    "My message is, 50 years from today we will still be producing oil."<br>

                    The challenge, he says, is to extract 50 per cent of the oil in the ground.&nbsp;<br>

                    "If we could do that our proven reserves would become almost 25 billion barrels, of which we have produced six billion. So if we find a solution, we will be able to tell people that our proven reserves are almost 20 billion barrels. And if you divide that by the current production, I bet the figure of 17 years will suddenly triple to 50."<br>

                    But Oman is not just improving its oil recovery factor, it is also looking for new oil fields, and Rumhy is equally confident of success on the exploration front.<br>

                    "We are learning more every day, not just on how to extract it, but how to find it. We are still being surprised by the complex geology of Oman, so I am sure we will find more," he says.<br>

                    "Oman geology is changing the theory of oil exploration, because for the first time we see oil where the old school of thought is telling us that oil shouldn't exist.<br>

                    "That is both challenging, expensive, difficult, but at the same time very promising."<br>

                    Oman recently signed a new concession agreement with Danish company Maersk to explore for oil near the border with Saudi Arabia. Rumhy says two more concession agreements are in the pipeline and should be signed within months.<br>

                    "These companies are expert at producing oil in difficult places, so we are very optimistic."</font></p>

