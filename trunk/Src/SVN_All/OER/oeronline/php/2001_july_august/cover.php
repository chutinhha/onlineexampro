                    <p><font face="Verdana" size="2">Rumhy is genuinely excited by the prospects for Oman's oil and gas industry, and says he gets enormous satisfaction from his work.<br>

                    But the 45-year-old father of two is humble when it comes to his own involvement in the huge strides Oman is making.<br>

                    With a PhD in Petroleum Engineering, he spent six years working with PDO and seven years teaching at Sultan Qaboos University before being appointed Minister for Oil and Gas in 1997.&nbsp;<br>

                    But he says he feels "lucky" to have the portfolio and the opportunity to work "on the front line".<br>

                    "The troops in the front line are only safe if they have a good general at the back, and the people's support," he says.&nbsp;<br>

                    "There are people supporting us all the way from school teachers to nurses, even the guys who sweep our streets - because we are very proud of our streets - and so the list continues. It's a national effort, and it's a good feeling.&nbsp;<br>

                    "We are not politicians, many of us, we are just people who work hard, trying our best to make sure that our kids' future is secure. Hopefully we will succeed."<br>

                    And then he smiles.<br>

                    "Seventeen years from today, if we're still telling people that we'll run out of oil in 17 years, I'll be a very happy man," he says, knowing full well that he will be.</font></p>

