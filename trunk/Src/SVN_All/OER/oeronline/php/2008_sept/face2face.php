<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">The Public Authority for Electricity and Water has done a study on clean coal 
technologies like solar and wind power. We see a lot of the potential for 
collaboration between India and Oman in these areas as Indian companies have 
carved an international reputation in these technologies. We are also looking 
forward to Indian companies being given the opportunity to present their 
credentials in the field of railways.<br>
<br>
With the Sultanate planning a railway line, we hope that railway infrastructure 
companies like IRCON and RITES from India will play a role in these projects. 
These companies have executed turnkey projects in difficult topographies similar 
to the mountainous areas of Oman in East Asia, Africa etc. Finally, we should be 
concentrating on frontier technologies like bio-technology, marine biology and 
telecom. Though telecom penetration in Oman is high, the market is migrating to 
higher technologies. As Indian companies have passed this stage they can come in 
as a valuable partner.<b><br>
<br>
How satisfying has it been for you to see trade relations between the two 
countries grow and are there any initiatives that you are looking forward to?<br>
</b>I have reason to feel satisfied with what we have achieved. There has been a 
100 per cent growth in trade from 2006 to 2007 and it is growing at fast clip 
this year. In 2007, 14 new Indian companies invested in Oman. We are in the 
process of setting up an Omani-India joint fund. The fund will start with a seed 
capital of $100mn and have spin off effects. We expect it to be operational by 
the end of 2008. Overall trade and investment growth has been satisfactory, we 
now have to ensure that the momentum is maintained and is built upon. </font></p>
