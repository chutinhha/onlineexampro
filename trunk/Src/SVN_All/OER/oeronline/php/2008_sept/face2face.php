<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">ON A GROWTH TRAJECTORY</font><font face="Verdana" size="1"><br>
HE Anil Wadhwa, Ambassador of India, Sultanate of Oman speaks to OER’s Mayank 
Singh about growing trade relations between India and Oman, emerging economic 
opportunities and the obstacles holding back trade<br>
<br>
</font>
</b>
<font face="Verdana" size="1">
<img border="0" src="face.jpg" align="left" hspace="6" vspace="4" width="300" height="265"></font><b><font face="Verdana" size="1">How would you rate economic ties between India and Oman? <br>
</font>
</b>
<font face="Verdana" size="1">The ongoing economic growth in India and Oman is creating new opportunities 
for both the countries. Sectors like tourism, infrastructure and finance are 
attracting a lot of interest from both the sides. There has been a convergence 
of economic interests in a number of sectors. India offers Omanis an attractive 
destination for long term gains both in the finance and manufacturing. In Oman 
there are new opportunities like alternate sources of energy, clean coal 
technologies like solar and wind power.<b><br>
<br>
</b>Indian companies are leaders in environment friendly technologies. For 
example we are among a few countries which has carbon capture expertise and this 
is going to be important in future for environment treaties.<br>
<br>
In infrastructure there are growing opportunities in tourism and management of 
tourism sites. New airports, a planned railway line and the port in Duqm are 
sure to interest our companies. There has been a sudden growth in the number of 
Indian companies coming to Oman in the last two years. Overall, there are eight 
Indian companies which are active in Oman. Trade has looked up as a number of 
new commodities have been introduced in the equation. Trade between the two 
countries which stood at $946mn in 2006 jumped to $1.8bn in 2007. This has been 
growing at 38-40 per cent and we expect two-way trade to cross $2bn mark in 
2008. <b><br>
<br>
<img border="0" src="face1.jpg" align="right" hspace="6" vspace="4" width="189" height="197">Is the balance of trade skewed heavily in favour of India or do Omani companies 
have commensurate opportunities in India?<br>
</b>Omani exports to India have gone up pretty sharply, last year they went up 
by 50-60 per cent and this has been quite satisfactory. Omani companies are 
finding new commodities that can be exported to India. Last year there were 
23,000 Omani tourists who visited India and we expect this figure to go up to 
37,000 this year. <b><br>
<br>
You mentioned new commodities that have started being traded; can you give us 
some examples? <br>
</b>On the Omani side there is a demand for limestone and chromium. The opening 
up of the mining sector in Oman has helped and recently we saw the Tata group 
entering into a joint venture with the Al Bahja group. Re-exports from the 
Sultanate has gone up. A number of commodities which were earlier bought from 
Dubai are now being purchased from Oman.<b><br>
<br>
The Indian Embassy has under your leadership initiated a number of measures like 
the Indo-Oman Business Forum, Omani-India joint fund and cultural activities. 
What is the role of the Indian Embassy in strengthening economic ties between 
the two countries?<br>
</b>We have a great role to play as most Indian companies do not have access to 
the decision makers in Oman. So the embassy steps in as a facilitator convincing 
the related authorities about the credentials of a company. A company backed by 
the embassy acquires a certain amount of credibility. It also gives people the 
assurance that in case of a dispute there is a fallback option. The Indo-Oman 
Business Forum is an initiative to galvanise Indian companies and to rally them 
together to explore business opportunities. It also works as a platform to get 
Omanis and Indian companies together in a social setting. The cultural events 
also serve as a platform to raise the profile of the community socially so that 
it can in turn lead to more business. <br>
<b><br>
What are the concern areas that are holding back trade between India and the 
Sultanate? <br>
</b>There are quite a few of them, for example simple things like air 
connectivity. When I arrived in Oman there were a number of destinations in 
India which were not serviced from Oman.<br>
<br>
Unofficial estimates peg the Indian expatriate population at being 500,000 
strong and unless there is good connectivity it is difficult for tourism and 
business to grow. Oman Air has lately increased frequencies to a number of 
stations in India. Jet Airways has started flying to Oman and we expect 
Kingfisher Airlines to start operations soon. We still believe that there are 
some new cities which should be introduced into the network. <br>
<br>
Shipping links between India and Oman is another area that needs to be 
strengthened. Shipping companies from India need to start using Omani ports 
regularly. For example instead of using other ports in the region they could use 
Salalah port. This will encourage Omani companies to use Indian shipping 
companies.<br>
<br>
Warehousing facilities is another area in which we would like Indian 
organisations like Federation of Indian Export Organisations (FIEO) to come in 
as this cannot be done by individual companies. These outfits should come 
together and open warehousing facilities in ports at Duqm and Sohar. These 
facilities could be used as a distribution hub for perishable products like food 
and vegetables. A refrigerated warehouse will ensure that these products are not 
lost plus these warehouses can be used as a hub to distribute goods to other 
countries on the Sultanates border.</font></p>

<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
