</font><b><font size="2" color="#CC3300"><font face="Verdana">International 
kudos for Bahrain’s economy</font></font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1">Bahrain’s economy is exceptional within 
the Gulf region and destined for success, an independent report has revealed. 
The ‘Growing Beyond Oil’ report, produced by the Conference Board, a 
not-for-profit research organisation based in the US, highlighted the rapid rise 
of Bahrain’s economy and demonstrated how the Kingdom had avoided many of the 
Gulf’s general economic pitfalls to position itself at the vanguard of economic 
progress in the region. The Conference Board confirmed Bahrain’s economy to be 
by far the most diverse and sustainable in the GCC. Finance and manufacturing 
are among the largest sectors, with oil and gas production contributing less 
than 20 per cent of GDP growth. This contrasts with other GCC countries which 
are heavily dependent on fossil fuel production. Bahrain’s diversification into 
the high-productivity sectors of finance and manufacturing was lauded by the 
Conference Board. The uniquely sustainable nature of Bahrain’s economic growth 
surge was also highlighted in the report. The basis of Bahrain’s economic growth 
is the dramatic increases in labour productivity. Data revealed that capital has 
played a relatively minor role in Bahrain’s success. This led the Conference 
Board to conclude that the Kingdom has “an unrealised potential for productivity 
growth without running into diminishing returns on capital investment.”<br>
</font><br>
<font face="Verdana" size="1">These productivity increases reflect Bahrain’s 
achievement of the highest rate of labour productivity in the GCC. Bahrain’s 
dominance is largely the result of two factors. The first is the deregulation of 
the labour market and liberal economic reforms introduced in recent years which 
has created ideal conditions for foreign investment in the Gulf’s strategic 
business hub. The second is Bahrain’s highly educated labour force and its high 
rate of both male and female participation. These were key areas emphasised in 
the report as preconditions for future economic success for the GCC economies. 
The roots of Bahrain’s success lie in its unique social and economic attributes 
within the region.</font></p>

