<img border="0" src="google.jpg" width="149" height="56" align="left" hspace="5" vspace="2">Google 
told to remove Belgian news reprints</b><br>
A Belgian Court has ordered US internet search giant Google to stop reproducing 
articles, photos and graphics from French and German language Belgian newspapers 
on its news site, according to media reports. <br>
<br>
Under the judgement handed down on September 5, the Brussels court ruled that 
Google must pay an Euro 1 million fine daily if it does not comply. The internet 
group was liable to pay another half a million euros per day if it failed to 
publish the ruling on its Belgian website google.be continuously for five days 
within 10 days of being notified of the court’s ruling. <br>
<br>
The case was brought by Copiepresse, which represents French and German language 
papers in Belgium including Le Soir and Libre Belgique. “The way in which Google 
News currently operates causes the publishers of the daily press to lose control 
of their websites and their contents,” the court ruled. <br>
<br>
A report from an expert witness, submitted in court, said that “the use of 
Google News circumvents the advertising of the publishers who get a considerable 
amount of their revenue from these advertisements.” <br>
<br>
It said that “Google News short-circuits many other elements” like references to 
copyright protection or the authorisation of the use of data. In a statement, 
Google said it “only found out about this lawsuit and the court’s decision 
...almost two weeks after the actual hearing. As a result we were not able to 
make our case directly to the judge.”<br>
