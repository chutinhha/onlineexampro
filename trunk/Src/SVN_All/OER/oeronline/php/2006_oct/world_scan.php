<b>Sony batteries recalled for a third time</b><br>
Toshiba has said it would replace about 340,000 laptop computer batteries 
worldwide, the third recall of faulty batteries made by Sony in less than 40 
days, according to media reports. <br>
<br>
The batteries, used in Dynabook and Dynabook Satellite laptops manufactured 
between March and May this year, could fail on the road because of problems with 
storing and transmitting power, Toshiba spokesman Keisuke Ohmori said. He, 
however, declined to estimate the cost of the move, saying it would not affect 
earnings, and would not say whether Toshiba was asking Sony to foot the bill. 
Toshiba said the faulty batteries would not cause fires. <br>
Although the batteries would die “only in rare cases,” that could happen 
regardless of how carefully they are used, Ohmori said. <br>
<br>
Dell Inc and Apple Computer had recently recalled almost 6m Sony-made batteries 
saying they could produce smoke and catch fire. Fujitsu and Hewlett Packard, 
which also use Sony batteries in their laptops, said no battery problems have 
been reported in quality control checks.<br>
