<img border="0" src="dp.jpg" width="150" height="93" align="right" hspace="5" vspace="2">DP World to increase global container handling capacity</b><br>
DP World, the world’s third largest port operator, is to boost container 
handling capacity at its terminals worldwide to 70 million units a year within a 
few years, from about 46.5 million at present, according a media report 
published recently. This will include a five million TEU (20-foot equivalent 
container units) addition at the port of Jebel Ali by the end of 2007, adds the 
media report.<br>
<br>
“Our capacity will increase to 70 million TEUs within a few years,” DP World 
senior vice president and managing director Mohammad Al Muallem is quoted as 
having said. He said Jebel Ali port’s container throughput was more than the 
total throughput at Indian ports.For the last four years DP World has recorded 
an annual growth of 20 per cent in the UAE, and this year it would add one 
million TEUs to its 7.6 million throughput of 2005. Under a $1.5-billion 
expansion plan, Jebel Ali will have a new terminal with a quay length of 2.5 km. 
“Once the new terminal is ready, we will be able to handle 13 million TEUs in 
this area,” Al Muallem is reported to have said.<br>
<br>
By July 2007, a 1.2-km quay will be completed and add three million TEU capacity 
to the port’s capacity. The rest will be ready by the end of 2007. DP World is 
involved in nine new projects worldwide, and these are expected to add a total 
of 24 million TEU capacity when fully completed. By 2010, the company will have 
13 million TEU extra capacity at the new developments.<br>
<br>
“We are still looking for new areas where we can have terminals,” Al Muallem is 
quoted as saying. In February, DP World acquired British ports group P&amp;O. Their 
combined throughput at 52 terminals in 30 countries was 36 million TEUs, with 
around 46.5 million of potential capacity.<br>
<br>
