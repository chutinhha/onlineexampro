<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>An eye-opener<br>
</b>
</font><font face="Verdana" size="1">
<b><br>
Pankaj Khimji, Director, Khimji Ramdas, visited South Africa recently as part of 
an industry delegation from Oman. Pleasantly surprised with the level of 
development the country has achieved, he recalls his experiences in South 
Africa.</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="byinvi.jpg" width="100" height="150" align="left" hspace="5" vspace="2">T</b></font><font face="Verdana" size="1">he 
visit served as a very good eye opener for the business community from Oman to 
have a look at what South Africa has to offer both in terms of export potential 
as well as inward remittance. The country right now is in a buoyant state: 
runaway inflation is a thing of the past; fiscal deficit is well managed; gold 
prices are soaring, the going has been extremely good for the mineral-rich South 
Africa for the past two years.<br>
<br>
All this has led to a spiral effect on the property prices boosted further by a 
lot of construction and property building that has been happening all across the 
country. The all-round development of the country is evident from the fact that 
South Africa will be hosting the football World Cup in 2010. The entire nation 
is in the grip of feverish activities, leaving little scope for service exports. 
A good sign, indeed!<br>
<br>
I fervently hope that the buoyancy rubs off on the neighbouring countries as 
well which are not as buoyant. The natural resources, talent pool, the skill 
sets are all of a very high order — second only to any developed country of 
Europe or the United States. South Africa enjoys a very competitive cost of 
manpower, decent literacy rate, and abundant natural resources. The flair and 
dexterity with which it is being converted into economic activity — read wealth 
generation — is mind-boggling.<br>
<b><br>
Relevant Lessons</b><br>
What is specially relevant for us, in Oman, is that the Middle East, which lacks 
natural resources other than gas and hydrocarbon, could very well benefit from 
this. There is a bonanza to be reaped, to put it mildly, especially from the 
agro-processing perspective, for instance, the conversion of sugarcane into 
sugar. Given the fact that the Middle East is a net importer of sugar and other 
agro products — which it imports from the European Union and the United States — 
perhaps we can start looking towards the Southern Hemisphere. 
</font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="byinvi1.jpg" width="400" height="214" hspace="5" vspace="2"></b></font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Till now, we are in the habit looking at Australia in the South. Why not scout 
around much closer home? South Africa, for instance, could very well be a 
destination worth exploring. Personally, the Khimjis, which is a commodity 
importing company, hopes to benefit from this.<br>
<br>
Another area of interest is the large availability of timber, building materials 
— from tiles to sanitary wares to steel structures, both readymade and 
pre-fabricated structures. If the quality of their products can be benchmarked 
with European Union or the United States, they can emerge as good sourcing 
destinations for us in the Gulf.<br>
<br>
Interestingly, South Africa has one of the most mature automobile and automobile 
components markets in the world, thanks to its one of the richest alumina 
industries and iron ores deposits. Believe it or not, South Africa is 
manufacturing cars for world auto giants like Mercedez Benz and BMW. Component 
manufacturers are also exporting to these giants from here. <br>
<br>
We also visited an industrial park solely catering to the automobile industry. 
South Africans have fully mastered the art of supply chain management. Auto 
seats in finished form are churned out in record time and put in an air 
container to be directly flown for fitting in the ready car factories in 
Stutgaart or Munich plants of BMW or Mercedez. It all takes precisely 24 hours!
</font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="byinvi2.jpg" width="350" height="206" hspace="5" vspace="2"></b></font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Business Focus</b><br>
As part of this delegation, we were keen on tie-ups in our respective areas of 
interest. This got into play during our one-to-one discussions with the local 
Chambers of Commerce and Industry. Of course we knew that this one off visit is 
unlikely to result in any concrete relationship. But it opened up vistas for 
both sides to explore areas of mutual advantage. With the catalogues and the 
list of contacts from Department of Trade and Industry, I am sure this will 
trigger a slew of correspondence between Johannesburg and Muscat — unlocking the 
mindset on what can be achieved from South Africa.<br>
<br>
From a global perspective, Oman is a very small trading partner for South 
Africa. Given this fact, this visit of the industry delegation will have a 
positive impact on bilateral relations between the two countries. The sheer 
importance — both response and hospitality — that the DTI and the Chambers 
bestowed on us, was touching, to say the least. Everything was just set right 
for us. This visit, I am sure, will set the ball rolling for cordial relations 
between the two great countries. <br>
— As told to Aparajita Mukherjee<i><br>
