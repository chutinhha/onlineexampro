</font><b><font face="Verdana" style="font-size: 16pt">Oman’s economy makes a 
robust growth</font></b><font face="Verdana" size="1"><b><br>
<br>
The Sultanate achieved tremendous economic growth in 2005, says the latest 
annual report of Central Bank of Oman</b><br>
<br>
</font><font face="Verdana" size="2"><b>O</b></font><font face="Verdana" size="1">man’s 
economy grew by 24 per cent in 2005 as against 14.2 per cent growth recorded in 
2004 as per Central Bank of Oman’s Annual Report 2005. The gross domestic 
product (GDP) increased to RO11.835 billion in 2005 from RO9.544 billion in 
2004. The year was marked by low inflation, large surpluses in fiscal and 
balance of payment positions, higher foreign exchange reserves, trouble-free 
domestic liquidity conditions with soft interest rates, major economic 
diversification and privatization programmes, and an excellent banking 
performance. All these factors led to an overall macroeconomic growth in 2005.
<br>
<br>
The fiscal position of Oman remained strong in 2005, with the fiscal accounts 
recording a surplus of about 2.6 per cent of GDP. The prudent use of fiscal 
revenue for growth supportive expansion in expenditure as well as for reduction 
of government debt and creation of larger foreign assets continued. As against 
budgeted deficit of RO540 million in 2005, there was a net surplus of RO303 
million.<br>
<br>
The Omanization exercise backed up by efforts to create more employment 
opportunities has made significant progress. The number of Omanis employed in 
the private sector as registered with the Public Authority for Social Insurance 
— showed a 13.2 per cent growth, from 87,064 in 2004 to 98,537 in 2005. Public 
sector continued to be the major source of job provider for nationals. In 2005, 
the total number of Omanis working in the public sector increased by about 5.1 
per cent from 104,141 in 2004 to 109,424 in 2005.<br>
<br>
The petroleum activities went up by 44.3 per cent due to high oil prices 
prevailing in the international market. It constituted 49 per cent of the 
national GDP as against 42.2 per cent in 2004. The average price of Omani crude 
was around US$50.26 per barrel in 2005, which meant an increase of 46 per cent 
over the average price of US$34.42 per barrel in 2004. The oil production 
registered a modest decline of one per cent in 2005 but high oil prices provided 
a noteworthy increase to income from oil exports. No wonder, the share of oil 
and gas sector in Oman’s GDP, exports, and net government revenue increased 
further to 49 per cent, 84.2 per cent, and 79 per cent, respectively, in 2005. 
On the other hand, the non-petroleum activities increased by 9.2 per cent, due 
to a strong 17.8 per cent growth in non-petroleum industrial activities. <br>
<br>
The exports have gone up by 35.3-per cent in value terms during the first 
quarter this year compared to the same period last year. Exports were worth 
RO2058.1 million at the end of March 2006 against RO1521.3 million during the 
corresponding period in 2005. The main factor which led to the jump was 38.5 
per-cent increase in oil and gas exports.<br>
<br>
On the domestic inflation front, the Consumer Price Index shows that consumer 
price inflation increased from 0.7 per cent in 2004 to 1.9 percent in 2005. The 
banking sector did well with capital levels of banks increased as percentage of 
risk-weighted assets from 17.6 per cent in 2004 to 18.5 per cent in 2005. The 
achievement is commendable as the minimum regulatory capital adequacy 
requirement was 12 per cent (reduced recently to 10 per cent to become effective 
under Basel-II). The gross non-performing loans (NPLs) as percentage of gross 
loans moved down to 7.3 per cent at the end of 2005 compared to 11.1 per cent in 
2004. The net profits of banks increased from RO79.4 million in 2004 to RO123.2 
million in 2005.<br>
