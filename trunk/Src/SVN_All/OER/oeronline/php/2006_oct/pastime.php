</b><font face="Verdana" style="font-size: 14pt"><b>Pool-ing in those hours<br>
</b></font><font face="Verdana" size="1"><b><br>
Karim Boukarroum, Head of Marketing, Alliance Housing Bank, meets up with Uma 
Devi Jadhav at the Second Cup and heads straight for the club…</b><br>
<br>
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>
<img border="0" src="past2.jpg" align="right" hspace="5" vspace="2">“I </b></font><font face="Verdana" size="1">shall meet you during my leisure time,” was Karim Boukarroum’s reply to my 
request for an appointment. As Head of Marketing at the Alliance Housing Bank, 
he has this privilege of working in the “much coveted” banking timings. However, 
this busy banker often needs to work beyond those hours.<br>
<br>
Karim has been in Oman for the last nine months, having done a stint in Bahrain 
for four years with O &amp; M advertising agency as an account manager and in Riyadh 
prior to that where he worked as an account executive with Publicis Graphics for 
over three years. Though he started his career in sales, this young executive 
has enjoyed all his varied work experiences.<br>
<br>
Just as he quickly adapts to his new assignments and job profiles, Karim is 
equally adept at numerous sporting activities such as tennis, table tennis, 
swimming and volleyball. His leisure time — from 5-8 every evening with his 
journalist wife and 10-month-old son — is absolutely important to him. “We go to 
the Cliff Club, Crowne Plaza Hotel, and generally chill out together as a 
family. My wife goes to the gym, and I play tennis or table tennis and really 
sweat it out. Then we are in the pool together with our little son. It is 
refreshing after a good game or work out. We enjoy a coffee or drink afterwards, 
and sometimes have dinner before heading back home.” He doesn’t play for 
competition, he emphasises. He wants to keep fit. That’s it.<br>
<br>
“I love volleyball but one needs a team, which doesn’t work out for me now. So I 
prefer tennis or table tennis — and if that is not possible, then solo 
activities such as swimming or gym are good enough. Exercise is a must for me as 
it helps relieve any stress left over from work!” How long has he been swimming, 
then? Pat comes the reply: “Forever! Ever since I was born! In Lebanon we swim 
together — the whole family and friends. We go for picnics and dive into safe 
rivers and lagoons. We always look forward to going into the water. Later we 
enjoy a barbeque and get back home. It’s the joy of being together, and yet it’s 
active outdoor living for everyone instead of just lounging around,” adds the 
very athletic gentleman.<br>
<br>
“In Lebanon, the season to be in the water is only from June to September. We 
can do water sports for only 3-4 months, while here it’s the reverse — so it’s 
even better! It’s hot, so there’s nothing more refreshing than the pool or the 
sea. I also like boating. I plan to buy my own boat someday soon. I love water, 
it is soothing, and I like to spend my leisure time around any water body,” he 
concludes.<br>
<br>
When one meets the extremely fit and cheerful Karim, there is no room for doubt 
that “leisure time” has to be active outdoor family time for anyone in his 
orbit.</font></p>
