            </font><font face="Verdana" size="5">
            Focus on core competence</font></b><font face="Verdana" size="1"><br>
            <b>
            <br>
            Dr. J. Retnakumar, Resident Manager, New India Assurance Company, 
            talks about how he has combined his passion and profession for a 
            social cause</b><br>
            <br>
            <img border="0" src="last_page_1.jpg" align="left" hspace="5" vspace="2"></font><b><font face="Verdana" style="font-size: 11pt">I</font><font face="Verdana" size="1"> </font>
            </b><font face="Verdana" size="1">was always interested in pursuing a career that catered to my core 
            competency — social cause through public relations. So in the year 
            1987, I deliberately chose an insurance job, moving away from my specialisation — post graduation in veterinary sciences, and 
            research in hypothyroidism in rabbits. <br>
            <br>
            I had three offers within a couple of months — toxico-pathologist in 
            a pharmaceutical company, an assistant professor in a college and 
            this insurance job. But I chose the last one, so as not to be 
            confined to some lab or campus. Of course I had to take a strong 
            stand, for my father was a professor in English. My grandfather too 
            was a post graduate in English Literature and rose to the position 
            of Director of Education in Thiruvananthapuram. In fact, right from 
            my childhood, I have been an extrovert — speaking up in classrooms, 
            participating in any and every social event. I was a very active 
            college union chairman and a university union councillor. I was 
            involved in a lot public activities.<br>
            <br>
            <b>Insurance Dynamics in Oman</b><br>
            Many a time, insurance may not cover the actual need-based risk. It 
            may talk of so many things that are immaterial to the concerned 
            person. In the last six months — I arrived in Oman on December 15 
            last year — I have formulated a new cover for the expatriate 
            Indians. It’s called Compass — Comprehensive Personal Accident 
            Security. Like the navigational aid, Compass gives the right 
            direction to the expatriates. It’s mainly for those coming from the 
            poorer section of society. If they are unable to go home for certain 
            reasons, and the Indian embassy certifies, then New India Assurance 
            gives them the ticket and expenses. We also come to their aid if 
            there is a medical emergency and the person needs to be evacuated. 
            Accident hospitalisation, death, permanent disability — all these 
            are covered under this policy.<br>
            <br>
            Now I have devised another cover called Better Health. This will be 
            a value addition cover — initially as a corporate group healthcare 
            policy for treatment in India and Oman. We are in the final stages 
            of clearance and launch. But let me sound a note of caution. Going 
            by the trend the world over, healthcare insurance will no longer be 
            a viable proposition. It’s not a profitable business proposition. 
            But Better Health is to promote Corporate Social Responsibility. My 
            decision has brought good rewards. Today I am helping forge new 
            equations in the insurance sector. Oman has no fixed tariff and the 
            rates are going down. On the other hand, the motor accident claims 
            are going up. Unhealthy competition too is affecting the market. 
            Healthy competition, with a balanced rate, should exist. Then the 
            customer can be served consistently. This has to be the goal. Else, 
            this becomes an unviable portfolio. If this trend continues, in two 
            years people may have to pay double — may be even more — than what 
            they are paying now. <br>
            <br>
            <b>Innovative Policies</b><br>
            Last year, I bagged the Best Divisional Manager award from my CMD 
            (Chairman-cum-Managing Director), with my division topping the 
            growth rate chart. Innovation has been my forte. In 1988, the 
            Karshaka Raksha (Farmer’s Protection) scheme for farmers in Kerala 
            was my brainchild. For the first time, one million farmers were 
            covered under a group policy. Another first, perhaps in the history 
            of insurance, was the banana plantation policy designed by me. In 
            the loan component, I designed an insurance cover, which specified 
            pest and disease cover also as part of the natural calamities.<br>
            <br>
            <b>Good leadership</b><br>
            My father influenced me the most during the formative years. My 
            bosses too gave me a lot of freedom, encouraging me to serve the 
            public cause. I have been very people oriented. Books did not serve 
            me as a motivator. This has been my basic character. See, I never 
            learnt music formally. But I sing well — mainly light music and 
            melodies.<br>
            <br>
            It’s important to create awareness in the minds of people about the 
            importance of insurance. That’s my goal. A good leader must be 
            result oriented and be innovative as well. That’s why I go for 
            designing tailor-made policies and products.<br>
            <br>
            My mantra: Redesign the existing insurance covers; come out with 
            newer products that would serve the public; bridge the gap between 
            the insurance company and the common man. The common man feels 
            insurance is a luxury item, rather than a necessity. That’s because 
            many a time insurance may not cover the actual need-based risk. It 
            may talk of so many things, which are immaterial to the concerned 
            person. But with an ever-increasing number of accidents and 
            uncertainties in the day-to-day life, insurance has become a 
            necessity for the rich and poor.<i>— As told to Abhijit Sinha</i></font></p>
