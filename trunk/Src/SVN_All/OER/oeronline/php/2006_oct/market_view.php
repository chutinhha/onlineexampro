<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><b><br>
</b><font face="Verdana" style="font-size: 14pt"><b>Substantial gains<br>
</b></font><font face="Verdana" size="1"><b><br>
Most markets across the GCC region have posted gains in their respective index 
values</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he 
MSM 30 index over the past month witnessed a substantial increase in its value, 
having grown by 8.86 per cent over the last four weeks. The three sector indices 
experienced gains in value as well, in addition to all of them showing higher 
levels than at the start of the year.<br>
<b><br>
Top Gainers and Losers</b><br>
The five companies that topped the list of gainers were National Carpet Factory 
(up 100 per cent), Transgulf Holding (up 43 per cent), Al Jazeira Services (up 
23 per cent), Oman Textile Mills Holding (up 23 per cent), and Gulf Investment 
Services (up 22 per cent).<br>
<br>
Conversely, the five companies whose share value witnessed the biggest drops 
over the past month were Cement and Gypsum Products (down 17 per cent), Oman 
Refreshment Company (down 14 per cent), Oman United Insurance (down 12 per 
cent), Oman Holdings International (down 11 per cent), and Al Khaleej 
Polypropylene (down 10 per cent).<br>
<b><br>
Volume and Turnover Toppers </b><br>
The most actively traded shares in terms of volumes this month were BankMuscat, 
Al Anwar Ceramic Tiles, Al Jazeira Services, Dhofar International Development 
and Investment Holding Company, and Alliance Housing Bank. In terms of turnover 
though, BankMuscat, Al Anwar Ceramic Tiles and Omantel topped the charts, being 
the only three companies with traded volumes exceeding RO 5 million.<br>
<br>
<b>Commentary</b><br>
Let us now compare the performance of the Muscat Securities Market with that of 
the rest of the GCC region.<br>
<br>
From the above table, we find that most markets across the GCC region have 
experienced gains in their respective index values, barring the Doha Securities 
Market and the Tadawul All Share Index (Saudi Arabia), whose indices dropped by 
4.13 per cent and 2.38 per cent respectively. The Muscat Securities Market 
registered a growth of 8.86 per cent over the last month, followed by the 
Bahrain Stock Exchange (up 5.26 per cent), the Kuwait Stock Exchange (up 5.14 
per cent), the Abu Dhabi Securities Market (up 3.33 per cent) and the Dubai 
Financial Market (up 0.58 per cent). <br>
<b><br>
Oman-US FTA: The Impact</b><br>
With the passing of the Oman-United States Free Trade Agreement (FTA) by the US 
Senate in late June 2006, another step has been reached towards the elimination 
of tariffs and other barriers to trade and investment between Oman and the US. 
Roughly, $1.2 billion worth of goods were traded between the countries in 2005.<br>
<br>
This is also a step forward towards the creation of a free trade zone across the 
Middle East by the year 2013, as envisioned by the US. As per the agreement, 
Oman will provide duty-free access to most US industrial and consumer products, 
excluding certain textile products. In addition, all tariffs related to 
agricultural products are to be minimised over the next 10 years. <br>
<br>
This agreement will benefit Oman in that the Sultanate will now face fewer 
barriers when seeking to export industrial products. </font></p>
