			<font face="Verdana" color="#FF0000" style="font-size: 14pt">
            <br>
            </font></b><font face="Verdana" style="font-size: 14pt"><b>The art 
            of unshrinking<br>
            </b></font><font face="Verdana" size="1"><b><br>
            Unshrinking is of great value to both the individual and the group, 
            including the corporate house. Here is a book, finds Uma Devi 
            Jhadav, that explains why, and how.</b><br>
            <br>
            The 
            basic concept of this book is not new at all. There are many 
            educators, teachers, parents and human resource professionals who 
            are already implementing the principles they talk about. However, 
            unshrink — the word means restoring to original amount or value — is 
            something all of us forget to do in our fast paced lives. We quickly 
            judge, compartmentalise and eventually ‘shrink’ people, places and 
            businesses, including ourselves, and end up doing untold damage in 
            both short and long terms to everyone and everything around. The 
            authors advocate a new way of ‘being’ with a view to succeeding in 
            all spheres.<br>
            <br>
            <img border="0" src="brcor.jpg" width="208" height="376" align="left" hspace="5" vspace="2">The first two myths are about us. We begin to feel that we are what 
            we do and that work comes first. Why constrict us with these narrow 
            beliefs? The principle in our lives should be that we are what we 
            can become, not determined by our current activity. Why can’t a 
            waitress, for instance, think of leading a country’s hospitality and 
            tourism industry, given the necessary education and inputs? Or why 
            can’t a mechanic, who knows his job well, dream of and train to lead 
            an automobile company? It is not necessary that people become more 
            stupid as one goes down the hierarchy. Why can’t we switch careers 
            and put life before work, and make ourselves happy and comfortable 
            in the bargain? Why should dyslexic children be put down as failures 
            when it is only one aspect of their school performance? The book has 
            empirical evidence collected by neuroscientists and leaders to prove 
            that there’s more to life than mere work — and being shrunk by it.
            <br>
            <br>
            The next two myths are about others. It questions the belief about 
            the all-important person in our lives, the boss, and the need to 
            keep plans secret. Superhuman bosses, combined with their natural 
            charisma, are true only in rare cases. If one looks carefully into 
            the history of big corporations, it’s not uncommon to find it being 
            put in the red by its top man! And we do find the same executives 
            moving to better positions after leaving companies bleeding! <br>
            <br>
            The authors showcase the irony of a standing ovation given by 
            students of Harvard Business School to Frank Lorenzo — “who, in the 
            decade of the 1980s, took Continental Airlines into bankruptcy twice 
            and led Eastern Airlines to its final demise on 18 January 1991.” 
            So, bosses are only human like us all and need not be put on 
            pedestals.<br>
            <br>
            Henry Mintzberg’s study shows that MBAs are no more likely to 
            succeed than the others. Withholding information is terribly 
            attractive but it leads to suspicion, fear and paranoia. It is best 
            to communicate and let the goals unify everyone. The feeling of 
            control that powerful people get from secrecy is an illusion. Gossip 
            increases in proportion to secrecy and, in the long run, one loses 
            trust.<br>
            <br>
            The last three myths are regarding unshrinking business. It is a 
            myth to say people obey orders. In effect, people do just as they 
            want. People govern themselves better and obey wholeheartedly only 
            if they understand and accept the principles. Rules are often built 
            up as scar tissue resulting from injury and stop people from 
            thinking. <br>
            <br>
            Organisations are not machines but a community, and must be treated 
            as such. Here, the authors give the example of the Swiss-UK medical 
            company, Generics Group, which monitors the contribution every 
            person — and not just its scientists and consultants only — makes to 
            the group. Administrators and secretaries are offered above average 
            salaries and a chance to own shares. Work itself should become the 
            reward. The need to entertain and threaten people to work is passe.<br>
            <br>
            Brad Hill, an unconventional pay consultant, feels people at the top 
            are self-motivated. According to him, unshrinking people is bringing 
            dignity and self-respect in work. Lastly, all change is not 
            necessarily good. Only good change is good! Sudden drastic 
            re-organisations and mergers are not all known to be successful. 
            Leading consultancy KPMG publicised the fact that 83 per cent of 
            mergers fail.<br>
            <br>
            While the two authors — activist and speaker Max McKeown and 
            journalist Philip Whiteley — offer us a new insight on how ideas 
            help change perception as well as stretch the potential of 
            individuals and companies, the book could have used a wider canvas. 
            It could have analysed why we shrink in the first place, to help 
            understand the unshrink process better!<br>
