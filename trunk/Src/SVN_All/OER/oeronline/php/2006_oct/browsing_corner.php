                ...Why is such an artificial barrier put between work and life? 
                Why do senior executives compete for long hours, travel miles 
                and stay nights away from their families? Why are phone calls to 
                and from family members during work hours viewed with such 
                suspicion or even banned outright? Why are working hours left 
                inflexible and closed to discussion?<br>
                <br>
                Insisting that a person works so many hours that they never see 
                their families cannot be good for longer-term economic growth. 
                Tired parents do not read bedtime stories. Children who do not 
                have this privilege fare less well academically and emotionally. 
                How can that be good for the economy or individual businesses?<br>
                <br>
                …There is a reason that so many amazing entrepreneurs had their 
                first breakthrough ideas while still at school or college -- 
                because they had time and room to think. Smart people will still 
                make bad decisions and create substandard designs if they are 
                too tired or stressed to think clearly. It takes even more space 
                to be able to create effectively. <br>
                <br>
                …Most of the people failing do not want to; most of those 
                causing pain would rather do good. We have been brought up to 
                believe that there is always a trade-off or a choice between 
                doing that which is good, and that which leads to success. Such 
                an assumption is wrong, and this is a tremendously liberating 
                realisation. One can quickly see that it does not only apply to 
                the matters of management and education that we have 
                concentrated on here. It has implications for trade, for 
                politics and for economic development…</font></td>
