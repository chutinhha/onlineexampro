<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<span style="font-size: 14pt"><br>
</span></b><font face="Verdana" style="font-size: 14pt"><b>Married to the Boss</b></font><font face="Verdana" size="1"><b><br>
For Jannat Shafaei and Hani Ali Mirza, it’s been a quest for the best in the F&amp;B 
outlets, finds Abhijit Sinha<br>
</b><br>
<b>
<img border="0" src="pers4.jpg" width="200" height="108" align="left" hspace="5" vspace="2"></b></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">W</font><font face="Verdana" size="1">ho 
is the boss? A question that, somehow, has never been a hurdle for Hani Ali 
Mirza and Jannat Shafaei when, as a newly married couple barely in their early 
20s, they decided to carve out a niche in the food and beverages catering sector 
in Oman. QED: Second Cup and Nando’s — one a café and the other a restaurant, 
and both first-time big league franchise outlets in Muscat. Canada brand topline 
bakery café Cinnzeo is a recent addition (at Shatti Qurum) to the family — 
rejoicing all the more with the recent arrival of a son.<br>
<br>
<b>
<img border="0" src="pers5.jpg" width="150" height="334" align="right" hspace="5" vspace="2"></b>It’s the family that stands behind the couple. The four siblings are the 
shareholders in the Bin Mirza International (BMI). Hani (Managing Director) and 
his brother, plus the two sisters. Jannat, technically, is an employee — albeit 
a topnotch one: Marketing Director. In practice, it’s been a Hani-Jannat show 
from the very start. With benign advice from Hani’s father who runs a group of 
companies — manufacturing, trading, contracting etc, where Hani had hands-on 
training (even got paid) during his school summer breaks. That honed his 
instinct for business “where I could deal more with customers — something not 
easy but more fun.” <br>
<br>
But it didn’t come easy. “The idea came when studying in London. I was a regular 
customer at Nando’s which has more than 100 outlets worldwide,” says Hani. First 
contact was in year 2000 to find out procedures of acquiring a franchise, but 
botched by the finance department of the family group. <br>
<br>
Three years down, Hani finally worked out the deal he wanted — and immediately 
started looking for a second option: “Two brands would give support to each 
other.” In other words, Hani was hedging his bets. “That’s when my wife and I 
decided to look out for a café — something that had always been in my mind.” <br>
<br>
Hani’s theme was to “change the perception of the people.” He qualifies this by 
adding, “There was no quality cafe in Oman — at least, not offering a high-end 
premium product.” So he got in touch with the Second Cup management in Canada. 
But before that, there were bouts of careful research in London, tense moments 
of negotiations in South Africa, followed by gruelling theory-cum-training (two 
months in South Africa for Nando’s, and a month in Canada for Second Cup) and 
tough tests. It was two years of shared sentiments, from the day Hani decided to 
get engaged to Jannat. Their goal was of doing something “on our own.” <br>
<b><br>
<img border="0" src="pers1.jpg" width="200" height="117" align="left" hspace="5" vspace="2">Planning 
a dream</b><br>
But what was the chemistry that brought them together? “I like humble people. I 
love that — and Hani has that quality. In fact, I found him more of a Bahraini. 
He thinks nothing of even cleaning the tables. That’s how we interact with our 
staff too,” says Jannat. Husband nods in agreement, and adds: “For us, all 40 
members are part of our family. We don’t treat them like employees. I go out 
with them. Even taken them to Canada and other places to attend conventions — 
part work, part holiday.”<br>
<br>
So where did it all start? Hani went from Oman to study Management in the 
European Business School, London. Jannat, whose roots are in Bahrain but had 
been living in UK for some 14 years after her father settled there, was also 
studying Management in Westminster Business School nearby. “Hani used to come to 
our library and we got to know each other through friends. He always used to 
dream that he will own cafes and restaurants — not the run of the mill stuff, 
but something different,” reminisces Jannat, a petite young mother who has a 
Masters in marketing, and utilises her talent in-house dealing with agencies 
while husband Hani handles the outdoor business.<br>
<br>
How do they find themselves now? Hani perks up to say, “I am a perfectionist — 
can’t sit still.” Jannat rolls her eyes, going, “Mmmm...” And adds, “Well, he 
wasn’t so in England. Now it’s because of the training we got in Canada. After 
coming back, we were not satisfied wherever we went. That added to our 
determination to give the best.” Any quarrels? “Lately, a lot more,” laughs 
Jannat. Hani merely grins but wisely keeps silent. “Though we are working 
together all the time, we make the most of going out and spending quality time 
together,” explains Hani — with Jannat nodding vigoursly. “Work — for me — is 
moving around, keeping check, meeting people, while she keeps tabs on brand 
building and marketing aspects.”<br>
<b><br>
<img border="0" src="pers2.jpg" width="100" height="124" align="right" hspace="5" vspace="2">Sense of adventure </b><br>
The Qurum outlet (near Capital Commercial Centre, Muscat) of Second Cup was the 
first to be launched. While Hani says it was October 2003, Jannat demurs and 
says it was 2004. Nando’s followed a month after. The love for the first-born is 
evident when Hani proudly states: “Only Second Cup in the Middle East that’s a 
drive-through.” And now they have recently opened another Second Cup outlet at 
Shatti Qurum. <br>
<br>
But there were bouts of anxiety prior to Nando’s launch. “We couldn’t open for 
three months, as the government clearance for hiring people was not coming 
through. Nando’s management people were here.” So they worked out a marketing 
strategy with their agency. Brand building was done through a ‘teaser’ campaign 
— billboard outside the empty building for Nando’s saying ‘Hang on Oman!’ <br>
<br>
“With less than a week before Ramadan, when all business slows down, we decided 
to go for it. The entire family pitched in — donning uniform and doing 
everything,” recalls Hani. Jannat chips in. “I don’t know about you but I felt: 
Oh my god, can we do this?” And they did it — even ran out of food on the first 
day, with people still in queue outside!<br>
<b><br>
Ups &amp; Downs</b><br>
For both, it has been a tremendous learning experience. Hani welcomes “the big 
spend on tourism” but bristles at the way “small business like ours are not 
taken care of.” He wants a re-look at the overall policy — not the way it is now 
seen as a “total package that mostly supports the big companies only.” His main 
grouse: “No government incentives for the small business people.”<br>
<br>
Of course, theirs is a family financed business. Been in the red some months? 
“Yes, of course. Profits are thin and expenses high. We have to pay royalty 
every month to our franchise owners from our net sales,” says Hani. So how did 
they manage? “By paying from our pocket. Things became real tight occasionally, 
but we managed. Mainly because we are a part of the family’s group of companies. 
Ours is a cash-oriented business but we managed to get credit at times.”<br>
<br>
With their third marriage anniversary coming up in September (Hani wants to add 
the engagement year to give longevity to their nuptial years), the obvious 
question is: What next? Plans both have aplenty — more outlets of Nando’s, 
Second Cup and may be even a Cinnzeo. But Hani and Jannat do not want to just 
concentrate on the F&amp;B sector. Their next target is to move into retail too.<br>
<br>
“But not immediately. May be two-three years down. Sure, a lot of activity in 
places like Sohar. But the right location has to be identified. Things are 
developing so fast. Today, it may be a good spot — but a big company may start 
next door. Picture not clear, a bit dusty for me,” optimises Hani, with Jannat 
nodding in vigorous assent. The shared sentiment is in being cautious. Another 
reason is the suddenly skyrocketing land prices. “For silly reasons,” fumes 
Hani. “Too much speculation, I don’t find that reasonable.”</font></p>
