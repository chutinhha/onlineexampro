      <p style="margin: 5px 10px"><font face="Verdana" size="1"><b>Lessons 
      learnt the hard way</b><br>
      Both like to think out of the box. Hani Mirza, just 28, wants to “retire 
      early and spend time with my children.” For Jannat, “becoming a role model 
      is a personal achievement.” She admits it was tough in the beginning, as 
      it was another country and she had grown up in a European country. “But I 
      found Oman to be much more advanced than many Gulf countries. But with my 
      husband as my boss, it was a lot easier to work.”<br>
      <br>
      Hani says that in a country like Oman, “experience isn’t everything.” 
      Personality is the main factor that counts — mostly when recruiting 
      people. “Happy, energetic, outgoing — those are the qualities I look for. 
      That person will think that though he did not have experience, these 
      people have taken him, trained him.” Reason is simple. “If I don’t help 
      them today, they won’t help me tomorrow.”<br>
      <br>
      To start a venture, says Hani, “the person must be comfortable, familiar 
      and passionate with a concept — and believe in it. Don’t choose it for 
      making money. Otherwise you will give it up the day you run into some 
      difficulty.” Jannat adds: “Weigh your target — the location, that’s the 
      key. Main thing is having the flexibility to adapt — but not the core 
      business.” Final word from both: “Spend on marketing. In Oman we don’t 
      take care for this aspect.”<br>
      <br>
      For girls wanting to start something of their own, Jannat’s advice is: “Go 
      for it.” For Hani, it’s the three-step American formula: “Take a concept 
      from a country. Package and develop it in a nice way. Spend a lot on 
      marketing it.” As a postscript, he adds wryly, “Then sell it back to us!”</font></td>
