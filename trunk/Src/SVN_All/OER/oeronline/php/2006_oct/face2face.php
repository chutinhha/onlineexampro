<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" style="font-size: 14pt"><b>It’s an era of digital 
lifestyle<br>
</b> </font><font face="Verdana" size="1"><b><br>
Vimal Sethi, Developer &amp; Platform Group Manager, Microsoft Gulf FZ LLC, in an 
exclusive interview with Akshay Bhatnagar, shares his thoughts on ‘producing’ a 
Bill Gates in Oman, the new Windows Vista, and challenges ahead for Microsoft</b><br>
<b><br>
<img border="0" src="face.jpg" align="left" hspace="5" vspace="2">How 
is the technology spectrum changing from Microsoft’s perspective?</b><br>
Microsoft believes the world is moving towards a digital lifestyle where 
devices, services and applications work together seamlessly. Windows Vista, Xbox 
360, new mobile devices and the emerging wave of software-based services will 
deliver greater connectivity and highly personalised experiences for consumers.<br>
<br>
We are heading for a quantum leap in rich, interactive, high-definition 
experiences. Technology has revolutionised how we listen to music, watch TV, 
play games, communicate, and manage and share personal and business information. 
In the coming times, a range of exciting innovations will unify the software, 
hardware and services in people’s lives — offering them even richer, more 
engaging and deeply connected experiences.<br>
<b><br>
What brings you to Oman?</b><br>
My trip to Oman is part of a special Microsoft initiative, Microsoft Technet &amp; 
MSDN Roadshow 2006, to help businesses and organisations in the region to create 
secure, robust and efficient IT infrastructures. We are visiting the UAE, 
Bahrain, Qatar and Kuwait along with Oman to showcase the latest innovations, 
tools and solutions, including Windows Vista and Office 2007, emerging from the 
Microsoft stable, to over 1,000 IT professionals and developers.<br>
<br>
One of the key messages that we often hear from our customers is that they want 
to reduce the complexity of managing their IT infrastructures, automate routine 
tasks and need more powerful tools to handle complex tasks more efficiently. 
This Roadshow 2006 gives us an opportunity to demonstrate to these IT 
professionals how Microsoft technologies can improve monitoring and management 
of processes, and help to maintain the performance and security of their 
systems.<br>
<b><br>
Talking of Windows Vista, what makes it an exciting product?</b><br>
Windows Vista is Microsoft’s next-generation operating system, scheduled for 
launch in a few months. It comes power-packed with features, offering to 
customers new advances in security, search capability and digital entertainment. 
Those who have used its beta version are unanimous that it brought clarity in 
their digital space, and helped them accomplish everyday tasks with greater 
ease. The users add they instantly find what they want, enjoy the latest in 
entertainment, keep their personal information safe, and stay connected at home 
or on the go. Its compelling consumer-oriented features include a sleek user 
interface, richer multitasking experiences and deep integration of search 
throughout the operating system. <b><br>
<br>
But why has its launch been delayed? Who should be blamed for the delay at 
Microsoft — developers or business strategists?</b><br>
It’s a tough one for me. We are placing lot of emphasis on launching an amazing 
product. We have learnt from our experiences that making the consumers go 
through beta testing helps in ultimately introducing a better product in the 
market.<br>
<b><br>
Are you not giving an opportunity to the competition with longer gaps in 
launching new versions of Windows?</b><br>
That’s a very valid point. The Vista launch has been a great learning exercise 
for Microsoft on this front. In future launches of Windows, I’m sure you are 
going to see a different kind of approach from us.<br>
<br>
We believe Vista is facing a bottleneck on the compatibility front, as it works 
in higher end machines like Pentium 4 only. Comment.<br>
<br>
The bottleneck in the usage of Vista is related to the graphics card. If the 
user wants to enjoy the full features of Vista’s operating user interface (OUI), 
the system requires the graphic card. It runs on most of the machines currently 
in use, but the user won’t be able to enjoy using Vista completely.<br>
<b><br>
What is the focus of your operations in Oman? </b><br>
Oman is a big investment area for us. Our Oman team has doubled in the last six 
months. We have around 15 people here and we are considering moving to a new 
facility. The focus area has been on building the channel and making the 
products available to our users. The fundamental investment area for us is to 
identify and forge partnerships with organisations that have expertise in areas 
such as training, licensing, contracting, technology solutions and services etc. 
We are looking at the creation of the right business eco system for Microsoft in 
the Sultanate.<br>
<br>
Besides, we are working very closely with the government. Our recent contract 
with the government for providing licensed technology is more of a partnership 
agreement. We endeavour to enable every government employee to be more 
productive and efficient with training and software application. <br>
<br>
I truly believe that Oman can also develop software solutions and export to 
other countries. Today, I met two local software developers here, and they said 
they wanted to develop their own software solutions company. I was quite happy 
to know that. The need of the hour is to encourage such talent and provide them 
with support on issues like project management, sales process, marketing and 
branding etc.<br>
<br>
How do you plan to identify and promote such local talent? <br>
We are focusing on community learning centres to impart IT awareness and basic 
training. But my favourite initiative in this direction is Imagine Cup, which 
will be launched this month. It is Microsoft’s global software development 
competition aimed at the student community. The theme for the contest is 
Technology for Education.<br>
<br>
We are going to engage all the major universities in the Gulf region including 
Oman. Students will compete in specific categories such as software development, 
digital art and IT solutions etc. We are going to partner with Information 
Technology Authority (ITA) and the Ministry of Education and key local companies 
for this event in Oman.<br>
<br>
In the Sultanate, I expect most of the universities to compete. It’s a great 
opportunity for the Omani students to showcase their talent and skills. I 
believe the top talents emerging out of this contest should get a chance to 
present their ideas to the private sector, especially investment companies and 
venture capitalists. We would like to incubate some of the best ideas. They 
could become the best software developers in Oman in the future, and could make 
the future IT board of the country to make it a software economy.<br>
<b><br>
On your regional operation front, how large is your team at Dubai?</b><br>
Dubai is our central hub for the region. Currently, over 300 people are working 
for Microsoft in Dubai.<br>
<b><br>
How many developers are there in your team?</b><br>
My team today has about eight developers from countries including Egypt, Jordan, 
Iran, the UAE, Kuwait and Bahrain — and we are hiring more local talent every 
month.<br>
<b><br>
When do you think your Gulf centre will be at par with you other global 
development centres?</b><br>
To be honest, I have an email in my inbox right now about this subject. The 
demand is there definitely. The way governments are investing in research and 
development activities, I think it will happen in the next few years.<br>
<br>
Globally, competition is getting intense for Microsoft — Google may venture into 
the Operating Systems segment too!<br>
<br>
Competition has always been there for Microsoft. It is healthy and keeps us 
focussed. In the past too people raised doubts about our success, but we have 
proven them wrong. As far as Google is concerned, we are keeping a special eye 
on them.<br>
<b><br>
What is the biggest challenge for Microsoft?</b><br>
Ours is a big company, with more than 70,000 people working for us. I think the 
challenge is on the front of managing growth, and meeting expectations of the 
analysts and stock market. A company with US$44 billion annual revenues cannot 
grow by 50 per cent every year! We have delivered revenues and profits 
consistently for the last 25 years, but I think our stock valuation is not 
justified. There are companies with no profit, and follow a linear approach to 
their product offerings. But their stock valuations convey a different picture.</font></p>

