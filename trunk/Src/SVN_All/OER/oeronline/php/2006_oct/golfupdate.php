<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Imagine the Scene... </b><br>
The shot in hand to the green finds you with a bunker in the way. The most 
common thought is what MIGHT happen if you do not hit the shot correctly, i.e. 
the ball finishes in the bunker! Instead of letting your concentration get 
affected by the bunker, focus on something ABOVE your intended target. It has 
been proven many times over that if you concentrate on the top of the flagstick, 
the treetops behind the target or even the white fluffy clouds in the sky, your 
attention will be diverted from the trouble. You will then be free to focus on 
what you WANT to happen, rather than what you DO NOT WANT to happen.<br>
Source: <a target="_blank" href="http://www.pga.info">www.pga.info</a>
</font></p>
