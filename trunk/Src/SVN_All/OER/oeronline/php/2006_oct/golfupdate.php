<b>
<img border="0" src="omanmobile.jpg" width="190" height="145" align="right" hspace="5" vspace="2">Visualising 
success<br>
</b></font><font face="Verdana" size="1"><b><br>
Golf is one game where visualisation can easily ward off nervous follies</b><br>
<br>
“90 per cent of golf is played from the shoulders up.”<br>
— Arnold Palmer <br>
<br>
Visualisation is the key to sports. For, isn’t it the most common technique 
adopted by professional sports people to optimise their performance? The 
emotional pattern of ‘nervousness’ does affect our game, and this includes our 
golf swing too. Ironically, nervousness forces us to induce exactly the same 
errors that we had set out to avoid. <br>
Let’s take the example of the first tee nerves. They can make us acutely 
self-aware and tense, resulting in a poor shot. By using relaxation and 
visualisation exercises, one can break the barriers of nervousness. What it all 
takes is a little bit of practice.<br>
<b><br>
Close Your Eyes... </b><br>
Imagine how you want to play a specific type of shot or hole you have difficulty 
with. Build up the process of actually playing it in your mind — from picking 
the club from your bag to teeing up. Imagine how you hold the club and swing 
through the ball, and see the path the ball takes. Imagine realistically where 
you would expect your best shot to land. Repeat this process completing the 
hole. Practise it regularly, and you will find it helps break any negative 
beliefs or habits around specific aspects of your game. The more you do it, the 
better it works. Of course, it takes a lot of time to practice this if you are 
serious about improving your game.</font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
