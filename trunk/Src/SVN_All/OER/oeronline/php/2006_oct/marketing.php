            <p align="justify" style="margin-left: 5px; margin-right: 10px"><b><font face="Verdana" size="4" color="#FF0000">
            <br>
            </font></b><font face="Verdana" size="4"><b>A market-finance metrics 
            creates better synergy<br>
            </b></font><font face="Verdana" size="1"><br>
            <b>We sat down with our regular OER marketing columnist Matt 
            Harrison to get his opinion on marketing assuming a greater 
            importance, guerrilla tactics, Rate of Investment, e-newsletters and 
            blogging</b><br>
            <b><br>
            Is marketing assuming a greater importance in business?</b><br>
            Without doubt. Marketing will be the most important area of 
            expertise for the next-generation of Oman-based business managers. 
            While marketing departments often struggle to measure effectiveness 
            and the related battle for internal credibility — particularly with 
            the guys in finance — several recent studies suggest that marketing 
            is making significant headway in proving its value within the 
            organisations. This rising recognition of marketing is due to fierce 
            market competition. Managers realise that being good just isn’t good 
            enough. We’re working in a hugely cluttered and confusing 
            marketplace — just about everything is available everywhere, all the 
            time. Smart managers understand that spreading the word about their 
            offerings is the only path to success. At the end of the day, the 
            success of any organisation is driven by one thing — whether or not 
            people choose to buy what you’ve got to sell. So, marketing is going 
            to play an ever greater role in any ‘thinking’ organisation.<br>
            <br>
            <b>You’re a big advocate of getting marketers up and talking at 
            events, aren’t you? Tell us more.
            </b>
            <br>
            I’m a big guerrilla marketing fan. If you’re short of cash, it has 
            to be about brains over brawn. Guerrilla marketers use energy, 
            ingenuity and effort — rather than lots of Rials. No matter what 
            line of business you’re in, speaking at conferences, seminars or 
            workshops is one of the most potent guerrilla marketing tactics I 
            know of. Research shows that, in selling, a demonstration is 50 per 
            cent more effective than the most glowing testimonial. A 
            presentation demonstrates what you and your business can do. It 
            shows your audience first hand how you think and work. Whether 
            you’re leading a seminar or delivering a keynote conference address, 
            you have the chance to meet people who want to learn about you and 
            what you have to say. In brief, you don’t have to find them — they 
            come to you. Remarkably, too few business people understand the real 
            marketing potential of public speaking.<br>
            <br>
            <b>Marketing accountability continues to be a hot topic. What are 
            your thoughts on this? </b> <br>
            The last major study I saw on this suggested that 68 per cent of 
            marketers were unable to determine the Rate of Investment (RoI) on 
            their initiatives. Let’s be clear, marketing RoI is a must for large 
            initiatives, and initiatives where it can be easily determined — for 
            example, direct mail or online marketing. But it can be a complex 
            and cost prohibitive process to accurately determine marketing RoI 
            on smaller campaigns. Don’t get me wrong, determining marketing RoI 
            is important. But what I’d like to see is marketing talk to finance, 
            and together discuss and identify the appropriate marketing measures 
            and who’s best suited to monitor these measures. I’d like to see a 
            member of the finance department take responsibility for managing 
            and monitoring marketing metrics.<br>
            <br>
            A marketing-finance partnership is beneficial on two levels. First, 
            it helps create important finance buy-in of marketing measurements. 
            Second, it can put responsibility for metrics in the hands of the 
            most qualified staff. Additionally, having these measures monitored 
            by a member of the finance department will eliminate the need to 
            promote marketing successes to the finance guys. It also means an 
            immediate awareness of failures, which probably puts the fear of 
            death into most marketers. Let’s be honest, the gulf between 
            marketing and finance is often the greatest between any two business 
            departments. Marketers need to forge a stronger relationship with 
            the guys in finance.<br>
            <br>
            <b>You’ve pushed corporate e-newsletters in your OER column. What 
            advice do you have for firms considering this marketing tool? </b>
            <br>
            If you own or run a business, then you’re probably an expert in your 
            field. At the very least, you’ll understand your product, service, 
            industry and the needs and interests of your customers. There is 
            even a good chance that you’re passionate about the work you do, the 
            product or service you provide. With an e-newsletter, you have the 
            opportunity to put that expertise and passion to work. At the same 
            time, you also have one of the most powerful, cost-effective 
            marketing vehicles on the Internet. An e-newsletter will help you 
            to:<br>
            <br>
            • Build your customer base.<br>
            • Establish your online business credibility and garner customer 
            trust.<br>
            • Establish you as an expert source of industry-specific 
            information.<br>
            • Open and maintain a communications channel with current and 
            potential clients.<br>
            • Strengthen your brand and develop consumer loyalty.<br>
            • Promote your inventory and highlight special offers.<br>
            <br>
            However, I read recently that only 11 per cent of corporate 
            e-newsletters are read thoroughly. So if you’re going to launch an 
            e-newsletter, the layout and look is the key. This means it has to 
            be designed to facilitate scanning. Time needs to be spent on the 
            upfront design, coding, testing and tweaking to see what your 
            end-users prefer. The e-newsletter shouldn’t be an e-mail version of 
            your Annual Report, but one that places an emphasis on brevity and a 
            compelling and easy to read layout.<br>
            <br>
            <b>You run a few Oman-based blogs — what are your thoughts on this 
            new marketing phenomenom?</b><br>
            A lot of marketers think blogging’s a new phenomenon when, in fact, 
            it’s been with <br>
            us since about 1994. Today, we’ve techie blogs, masters swimming 
            blogs, manufacturing blogs and TKM — Ernst &amp; Young Big Business Idea 
            Competion blogs. There are also news and commentary blogs, sites 
            packed with great links, tips, ideas and arguments that were until 
            recently the preserve of established news outlets. I love the 
            immediacy of blogging, it’s talk radio re-invented. Blogging’s 
            changing the media world. It’s a publishing revolution more profound 
            than anything since the printing press. I’d thoroughly recommend 
            blogging to any marketer, but be prepared, a new blog is born every 
            second of every day and the blogosphere is doubling in size every 6 
            months or so. It’s easier and more appealing than ever to get into 
            the game, but your learning curve — as for any new venture — will be 
            as steep as Kilimanjaro.<br>
            <br>
            <i>Matt Harrison is a Muscat based marketing consultant.</i></font></p>
