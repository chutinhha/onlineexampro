<p align="justify" dir="ltr"><font face="Verdana" size="1">
<br>
<br>
</font><font face="Verdana" size="5">
<b>Now is the Time<br>
</b></font><font face="Verdana" size="1"><br>
<b>Sridhar Sridharan, Tax Partner with Ernst &amp; Young, Oman, delineates the 
factors that make the Middle East a hot destination for investments</b><br>
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>
<img border="0" src="byinvet.jpg" width="100" height="102" align="left" hspace="5" vspace="2">A</b></font><font face="Verdana" size="1">s 
both worldwide energy demand and commodity prices reach record levels, it stands 
to reason that the Middle East — a region with more than half of the world’s 
crude oil reserves and one-third of its natural gas reserves — would offer the 
highest profit prospects. There are a variety of converging factors that make 
opportunities for investment in the Middle East more enticing than ever before. 
Even those companies that have never before ventured onto foreign soil are 
taking a long, hard look at the Middle East. Quite simply, now is the time to be 
here.<br>
<b><br>
Population Explosion</b><br>
The Middle East encompasses more than a dozen oil producing countries with 
distinct economic and social characteristics, but with at least one 
characteristic in common: young, rapidly growing populations in need of 
employment. According to the World Bank, the Middle East/North Africa region had 
the world’s second-fastest growing population from 1980–2002: 2.6 per cent vs. 2 
per cent for the South Asia, 1.4 per cent for East Asia/Pacific and 1.1 per cent 
for the US. At 2.7 per cent, only the impoverished Sub-Sahara Africa region grew 
more rapidly.<br>
<br>
The World Bank projects that this pattern will continue through 2015, with 1.7 
per cent for the Middle East/North Africa region vs. 1.4 per cent for South 
Asia, .8 per cent for both East Asia/Pacific and the US.<br>
<b><br>
Wanted: Foreign Investment</b><br>
With Middle East governments aware that they can no longer provide full 
employment to their citizens, the enormous need for private-sector jobs for 
young, rapidly growing populations is fundamental to the efforts at attracting 
foreign investment. The primary target of these investments is not foreign money 
but the technology that comes with them and creates long-term economic 
opportunities for the citizens of these countries.<br>
<br>
While the greatest pressure to attract outside investors is coming from inside 
the region, the oil-exporting nations of the Middle East also face tremendous 
political pressure from oil-consuming nations to increase exports. With 
production at near capacity (mostly), huge investments in additional energy 
infrastructure will be needed to accomplish this goal.<br>
<b><br>
Incentives Abound</b><br>
What are Middle East governments doing to attract foreign investment? The 
efforts encompass a combination of substantial changes in their tax, legal, and 
regulatory environments, all designed to make investment more appealing.<br>
<b><br>
Tax Rates Falling Across Region. </b><br>
• Saudi Arabia recently passed a new income tax law to replace its 54-year-old 
predecessor. Among the highlights, corporate income taxes are now fixed at 20 
per cent (earlier 20-30 per cent) and a tax rate applicable to natural gas 
activities of 30 per cent. In addition, companies investing in the natural gas 
sector will also be subject to a natural gas investment tax where rates range 
from 30-to-85 per cent, based upon the project rate of return.<br>
<br>
• In Egypt, a new corporate tax system has replaced the one that has been in 
place since 1981. Under the new law, the highest tax rate fell from 42 per cent 
to 32 per cent. Numerous special incentives are in place for oil and gas 
companies.<br>
<br>
• In Kuwait, a proposed new law would reduce the maximum tax rate to 25 per 
cent. <br>
<br>
• In Oman, tax rates for all companies registered have been set at 12 per cent.
<br>
<br>
• Qatar’s Ministry of Finance is reviewing its current taxation system; it is 
expected that a new tax law will be enacted soon that will include additional 
tax incentives for foreign investors, as well as a reduced tax rate. <br>
<b><br>
Liberalizing Rules</b><br>
In addition to reducing corporate tax rates, many Middle East countries are 
liberalizing ownership regulations, residency rules, and trade laws to allow 
foreign companies to set up shop more easily.<br>
<b><br>
Single Economic Bloc</b><br>
A further development in the region is the continued push to create a single 
economic bloc with uniform economic policies. This initiative has already given 
rise to a common customs union in the region and plans are afoot to adopt a 
single common currency in the region in the medium term future. Harmonization of 
rules across the region will make it easier for investors to adopt a regional 
focus for their future activities. <br>
<b><br>
Downstream Opportunities</b><br>
Since oil was first discovered in the Middle East in 1938 and production kicked 
into high gear after World War II, the majority of participation by foreign 
companies has been in the upstream sector, with Western companies managing the 
region’s most precious economic assets: huge oil and gas reserves. In the 1970s, 
in order not to become too dependent on the West, many governments nationalized 
practically all oil production, and placed tight constraints on the nature and 
limits of foreign participation in the upstream sector. <br>
<br>
This history explains in part why the largest opportunities for foreign 
companies today are found in the midstream and downstream sectors. Across the 
region, numerous opportunities are emerging for foreign companies and their 
suppliers to build, own, and manage liquefied natural gas (LNG) plants, 
pipelines, gas processing plants, refineries, chemical plants, power facilities 
and other infrastructure facilities. For example: <br>
<br>
• The natural gas business, in particular, is booming. With huge reserves of 
non-associated gas but lack of infrastructure to transport and consume it, 
countries such as Qatar, Egypt, and Oman continue to make enormous investments 
in LNG facilities and natural gas pipelines. Saudi Arabia has also launched a 
number of projects under its Saudi Gas Initiative where partnering arrangements 
with foreign companies in the upstream sector has been allowed for the first 
since the creation of Saudi Aramco.<br>
<br>
• Large volume gas-to-liquids conversion is rapidly moving from a conceptual 
stage to reality. Qatar is currently developing or planning a number of gas 
processing projects to convert methane-rich associated gas into middle 
distillates such as kerosene, naphtha, gas oil and other products. Other 
countries are waiting in the wings to evaluate the lessons learned from the 
Qatar projects before catching their own GTL (gas to liquid) train. <br>
<br>
• Attracted by the proximity and relatively low costs of feedstock, many Western 
companies are investing in the region’s refineries and chemical plants. With 
growing demand for petrochemical products in South Asia and the Far East, there 
is a compelling case for investment in petrochemical projects in the Middle East 
region.<br>
<br>
• Water and power requirements present opportunities. A growing population and 
an increasing industrial base present many opportunities for the development of 
independent power and desalination projects across the region. To meet the power 
requirements of Saudi Arabia alone, new build power projects, estimated at US$ 
15 billion, will be required to be developed by 2010. <br>
<br>
• Construction companies have the opportunity to participate in many of the 
major development projects on the drawing board across the region.<br>
Small Players Welcome<br>
<br>
To some, the discussion of foreign companies doing business in the Middle East 
undoubtedly conjures up images of global giants making multi-billion dollar 
investments. While multinational majors have historically played a major role in 
the industry and continue to do so, the opportunities now for small and 
mid-sized companies to participate in the booming Middle East business are 
greater than ever before.<br>
<br>
In fact, many Middle East governments have shifted their emphasis away from the 
global giants and begun courting smaller, leaner organizations. Western 
companies with a competitive advantage over their larger peers—whether it’s a 
low-cost structure or an edge in developing and deploying technology—are prime 
candidates to make the short list of participants in attractive investments in 
the Middle East.<br>
<b><br>
Taking the Plunge: First Steps</b><br>
While global business is always a complex undertaking, entering the Middle East 
region presents a unique set of challenges for any company. Consequently, it is 
imperative to map one’s strategy as clearly as possible and answer some very 
basic questions:<br>
<b><br>
Where do you want to go? </b><br>
Some countries, such as Bahrain and the United Arab Emirates, have traditionally 
been open to foreign investors, while others, including Egypt, Kuwait, Oman, 
Qatar, and Saudi Arabia, continue to open their doors. But there are newcomers 
with huge potential, including Libya and Iraq, ready to make their entrance onto 
the world business stage for those who understand how to do business with them.
<br>
<br>
What do you know about your potential target?<br>
It’s common for Westerners to paint the Middle East with a broad brush, as a 
single homogeneous region, but it’s only after their differences have been 
understood that Western companies can fully appreciate the characteristics these 
nations share, particularly from business, economic and religious points of 
view.<br>
<br>
While the nations in the region share many cultural characteristics, and 
continue to promote a drive to harmonize rules and regulations, it’s important 
to realize that each country is a unique entity with its own set of 
opportunities and challenges.<br>
<br>
Some countries in the region are wealthy; others are poor. Some have modern 
legal and business environments; others do not. Some have a long history of 
working with Western companies, while others have only recently begun 
encouraging foreign investment.<br>
<b><br>
What are the factors that you should address?</b><br>
Middle East, with its combination of political, economic and cultural issues 
requires careful analysis of several factors. For those who do their home work, 
the Middle East will continue to present attractive investment opportunities for 
the foreseeable future. However, companies must plan their regional market entry 
wisely in order to reap the rewards of their investments for years to come. </font></p>

