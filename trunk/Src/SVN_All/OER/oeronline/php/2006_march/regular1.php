</font><font face="Verdana" style="font-size: 16pt">
Creativity leads to better management</font><font face="Verdana" size="5" color="#FF0000"><br>
</font></span><font face="Verdana" size="1">
<b>
<br>
Find out how a creative person can excel in management with Omar Alwi, the 
Country Manager of American Express in Oman and Yemen</b></font></p>
<p align="justify" dir="ltr">
<font face="Verdana" style="font-size: 14pt; font-weight: 700">
<img border="0" src="atthehelm.jpg" align="left" hspace="5" vspace="2">L</font><font face="Verdana" size="1">ife is 
unpredictable. I did my bachelor’s degree in commercial arts from the UK but 
instead of charting out a career in a creative filed, I ended up in customer and 
marketing management, selling a financial product in different markets! But I 
have no regrets. I have enjoyed every bit of my career with the American Express 
ever since I joined the company way back in 1992, moving away from handling 
reservations with Alitalia. <br>
<br>
At American Express, I initially joined in the credit assessment function and 
later took upon the role of a territory manager in the eastern region in Saudi 
Arabia. In October 1999, I was posted to Oman to handle the market here, besides 
taking care of the Yemen market, which happens to be my homeland. <br>
<br>
I have been monitoring Oman’s economy from close quarters. Take my word, the 
market here is booming and gearing up for a much higher scale of development in 
the near future. Think of any sector -- tourism, real estate, stock market, 
banking or education -- Oman is headed for a major upswing. I am excited by the 
new business opportunities created by the market expansion in the recent times. 
This is perhaps the best time for a manager to prove his mettle and take his 
company’s performance to new heights.<br>
<b><br>
Team Performance</b><br>
In the changing market dynamics, the manager’s role of setting up the requisite 
infrastructure and managing the team effectively becomes very crucial. I 
strongly believe that the key to achieve business goals and management 
objectives is to make the team perform at the optimum level. To make the team 
click, I think, a manager can follow two different strategies. One is to adopt a 
hard approach and use an autocratic style. I don’t believe in this method. This 
kind of approach might deliver results in the short run but in the longer run it 
might prove to be counter-productive to the company. <br>
<br>
I prefer the second approach, which is based on treating the team members in a 
gentler manner. I try to understand their problems and strive to solve them. I 
boost their morale by encouraging and motivating them to achieve their 
individual goals by offering valuable tips. I strongly believe that if a manager 
can take his team along with him successfully, the task of achieving the desired 
results becomes that much more easier. The staff members are the real assets, 
which can make or break a company.<br>
<b><br>
Work Experience</b><br>
We see many a management graduates come in straightaway as managers, leading a 
team without any real time work experience. In comparison, I think managers who 
have gone through the grind themselves make better managers. Take my case. I 
have come up the rungs in a hard way by working in different positions in 
different functions across markets. I readily connect with the duties and 
problems of my team members as I have experienced all that myself. At American 
Express, my superiors treated me very nicely. And I treat my staff in similar 
way. So this kind of healthy work atmosphere spreads across the organization. I 
know from my experience that if I treat my team nicely, they will perform 
better. <br>
<br>
I have been working on creating the perfect team in Oman. We aim to take 
American Express to the new emerging markets like Sohar, Sur and Salalah. With 
the rapid growth in economy, people are shifting their allegiance to plastic 
money. This gives us an enormous opportunity for business expansion. <br>
<br>
I have seen many individuals who spend so much time on their professional lives 
that they start losing on the personal front. I believe that one should maintain 
a healthy balance between the professional and personal lives. That’s why, 
despite my busy schedule, I do make it a point to spend quality time with my 
four kids. I regularly take them out on weekends. <br>
<br>
Only a good, healthy body can take care of the rigours of a tough professional 
life. So I work out at a gymnasium, and do walking, jogging and swimming 
regularly, to keep myself fit and in perfect shape. <i>– As told to Akshay 
Bhatnagar</i></font></p>

