<p align="justify" dir="ltr"><font face="Verdana" size="1">The Qatar proposal is 
yet to be fully vetted by the executive committee. According to Patel, Al 
Omaniya briefed BDO Jawab Habib to explore possibilities of opening shop in the 
hire purchase and leasing business model which they excel in, in Oman. 
Obviously, Patel and company prefers to stick to the netting. The Bahrain 
proposal fell through due to the absence of a vibrant domestic market. “No 
doubt, Bahrain is the financial center. But most of the activities have nothing 
to do with the domestic market,” explains Patel. <br>
<br>
Qatar, on the other hand, is booming and a proper financial surveillance system 
is in place. Therefore, the downside risks are almost negligible. Moreover, 
given its own track record over the past eight years, finding a suitable local 
partner in Qatar will not be difficult. The seed capital for the proposed joint 
venture is expected to be in the region of RO 5million, which is no big deal for 
Al Omaniya. <br>
<br>
Its financial strength can be ascertained from the fact that it has already 
fulfilled the Commercial Companies Law provisions of the Sultanate of Oman with 
regard to legal reserves. Under this statute, 10 per cent of the net profit for 
the year is required to be transferred to the legal reserve until the 
accumulated balance of the reserve is equal to one third of the company’s paid 
up share capital. Once this threshold has been achieved, the company may decide 
to discontinue such transfers. <br>
<br>
Al Omaniya’s authorized share capital is RO 10,000,000, of which RO 6,487,971 is 
authorized and fully paid up. The legal reserves, as on 31 December 2005, is in 
excess of RO 2,162,657. While the legal reserves cannot be accessed for 
distribution among shareholders, the 10 per cent provisioning of net profit will 
not be in force from the current financial year 2006. Simply put, the company 
will have additional resources for distribution even if it maintains the current 
growth momentum of 6.95 per cent in net profit. <br>
<br>
Thanks to the improved credit worthiness of Omani customers, Al Omaniya’s 
non-performance assets have come down sharply. Besides, it has made provision 
for a 106 per cent coverage against non-performing assets. Over-cautious? “Not 
at all,” avers Patel. With better credit management and higher provisioning, Al 
Omaniya is judiciously improving and increasing its reserves for a better 
tomorrow. Not a bad deal!<i>- Ramesh Kumar</i></font></p>

