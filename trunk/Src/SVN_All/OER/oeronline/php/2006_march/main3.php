<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>OER traces the 
global ambitions of Al Omaniya Financial Services</b></font></p>
<p align="center" dir="ltr"><font face="Verdana" style="font-size: 14pt"><b>
<img border="1" src="alomanya.jpg" width="300" height="143" hspace="5"></b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt"><b>
I</b></font><font face="Verdana" size="1">t 
has been a steady path to development for the Muscat-headquartered Al Omaniya 
Financial Services SAOG ever since it came into existence in 1998. The company 
has registered all round growth in every facet: profit has shot up by 500 per 
cent for the year ended December 31, 2005. Revenue grew by 7.3 per cent. Assets 
have grown by 308 per cent. Net worth gained 320 per cent (See graph below). 
Above all, as far as the shareholders of this Muscat Securities Market-listed 
financial services company are concerned, they never had it so good: a 
consistent 20 per cent cash dividend for the past three years (2002-4). Thanks 
to the upbeat economic conditions, the 2005 cash dividend has been hiked to 21 
per cent – one additional percentage point over and above the previous year. 
Indeed, these are glad tidings for the Al Omaniya top brass.<br>
<br>
Al Omaniya is now looking beyond Muscat. It is eying Qatar, the country that 
enjoys the highest per capita income in the entire Gulf region. “There could be 
a whale of opportunity to sink our teeth in,” says Aftab Patel, Chief Executive 
Officer of the company. The former BankMuscat senior executive has roped in BDO 
Jawab Habib, the reputed financial and management consultants, to carry out a 
thorough study.<br>
<br>
BDO Jawab Habib findings have just arrived in and Al Omaniya bosses are in the 
process of analyzing the same threadbare. But a sneak preview of summary 
findings enables Patel to confess that the prospects look “bright”. Yet, the 
bespectacled financial expert prefers to tread cautiously. “Commissioning a 
study does not warrant that we are opening an office soon,” he hastens to add. 
His approach is understandable given the fact that two years ago, Al Omaniya was 
nursing ambitions of going into Bahrain, the financial hub in the region, with 
the same zeal. A similar exercise was carried out, but ultimately, the Board 
shot down the proposal. Patel has not forgotten those days.
