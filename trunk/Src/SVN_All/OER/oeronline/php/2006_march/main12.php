</font><b><font face="Verdana" size="5">Mobile gaming contest rocks Oman</font></b><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="3"><b>
<br>
</b></font><font face="Verdana" style="font-size: 14pt"><b>“T</b></font><font face="Verdana" size="1">he 
Gamer Challenge 2006,” organised by Oman Mobile in association with Sultan 
Qaboos University (SQU) Communications and Information Research Centre, has 
generated immense interest among the mobile gaming enthusiasts in the Sultanate. 
Last year also, the contest had evoked widespread enthusiasm among the mobile 
gaming community in Oman. <br>
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>
<img border="0" src="omanmobile.jpg" width="150" height="91" align="left" hspace="5"></b></font><font face="Verdana" size="1">The 
contest entails development of mobile game concepts by the student groups and 
then sending the winning team for advanced training in mobile game development 
abroad. Last years’ winners were sent to China to learn the finer points of the 
mobile game development from the professional game developers. The winning entry 
‘Forts Adventure’ was developed into a full-fledged mobile game and it was 
launched by Oman Mobile on its network in December last year. The game concept 
was based on the character ‘Khalfan’ who learned many anecdotes from his 
grandfather about forts encompassing a lucrative treasure. He decides to hunt it 
out in the numerous local forts and castles. However, he eventually comprehends 
that the treasure his grand father meant was the wonderful architectural 
endeavours of his great grandfathers’ enchanting the forts and castles. <br>
<br>
This year the contest has been introduced on a much wider canvas. The ten 
nominees were selected in February among the participants from colleges 
operating in Muscat Governorate: namely the Caledonian College, Muscat College, 
Middle East College, and Modern College of Business &amp; Science in addition to 
Sultan Qaboos University. Oman Mobile has selected this time the community 
interaction in the voting for the winning group. <br>
<br>
This trend will encourage the participants to excel as well creating an 
interaction and motivation for further enhancement and betterment.<br>
<br>
This year the winners will be rewarded with an all expenses paid trip to 
Amsterdam, Nokia Communicator 9300 and Sony PSP with one game. The winning group 
will be announced in 19 March 06 at a special ceremony planned to be organized 
at the SQU, as well honouring the other participating groups where the top three 
as well public audience will be awarded prizes.<br>
<br>
