industry worth US$1.5 billion globally and growing a fast pace. According to ABI 
Research, &quot;recent efforts by mobile gaming giants to develop an open gaming 
architecture in order to reduce development frag</font><font face="Verdana" style="font-size: 14pt"><b><img border="0" src="omanmobile1.jpg" align="right" hspace="5" width="200" height="136" vspace="3"></b></font><font face="Verdana" size="1">mentation 
will open the doors to more content for consumers an</font><font face="Verdana" size="1">d 
overall higher revenues for game downloads&quot; in the mobile market, and an 
increasing consolidation between the traditional videogames market and the 
wireless sector adds weight to the company's assertions.<br>
<br>
If 2005 was the year that mobile gaming went 3D and broadband, 2006 will be the 
year that mobile gaming gets big. Not in terms of the size of the cell phone 
screen, of course. But in terms of game capacity, pricing, audience and the size 
of publishers entering the market. While 2006 will see changes in terms of the 
mobile sector's audience and the hi-end games available, one thing that won't 
change from 2005 are the two highest hurdles that developers will face - gaining 
access to the cell phone deck and porting games to an ever-growing number of 
handset models.</font></p>

