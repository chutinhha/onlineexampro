            <br>
            </font><font face="Verdana" size="4">
            Roddick on the rampage</font></b><font face="Verdana" size="1"><br>
            <br>
            <img border="0" src="brow_cor.jpg" width="150" height="103" align="right"></font><font face="Verdana" style="font-size: 14pt"><b>A</b></font><font face="Verdana" size="1">nita 
            Roddick is better known for her book, The Body Shop. She now comes 
            out with a new title that is bound to make people ponder a lot. 
            Where are we all heading towards? Where, indeed, do we all wish to 
            reach in the end? Do we really want to do, think, eat and drink as 
            per the role models thrust up in front of us? Yes, by those 
            advertisers and large corporations!<br>
            <br>
            And, in the end, if we really don’t want to do all that we are being 
            persuaded to do, then we need to take up the challenge, to break 
            away from the stereotypes, become aware of how and why things are 
            happening and take responsibility in our own small ways to think and 
            feel the way we want to be. Let’s not get bulldozed by large 
            corporations in the name of globalization.<br>
            <br>
            Roddick feels democracy and capitalism do not go hand in hand. It is 
            only a myth. In reality, democracy and market mechanism do well 
            together. She has done an interview with the Ruckus Society, which 
            trains activists to make their voices and views more dynamic, 
            inspiring and educational. She also deals at length on how 
            globalization is failing and how the World Trade Organisation (WTO) 
            is going to have disastrous side effects.<br>
            <br>
            She gives examples of how developed countries are following 
            protectionist policies themselves while preaching, parroting, and 
            getting developing countries to open their economies in every area, 
            and at the same time, preventing technology from reaching them. It 
            is clear that showing a carrot to the developing world and getting 
            them to import isn’t going to work in the long run. <br>
            <br>
            Unless the countries are able to use those imports productively, 
            they will suffer and stop further imports, forcing a worldwide 
            recession. <br>
            <br>
            The second and third myths that “Globalisation ends poverty and 
            hunger” is torn apart by Roddick through photographs, tables and 
            articles. She’s emphatic that economic globalization creates wealth 
            only for the elite who benefit from mergers, global scale 
            technological and financial activity. Corporate crimes kill far more 
            people and costs taxpayers far more money than street crime, she 
            contends. <br>
            <br>
            The plight of the Burmese people -- repressed by their own military 
            regime -- or that of the Mexican people, through foreign funding by 
            a super power, may induce some readers to volunteer in a small way 
            with the various voluntary groups listed at the end of each chapter! 
            The changing face of Ladakh under the façade of the “global village” 
            is similar to what I have seen in other hilly regions of India as 
            well.<br>
            <br>
            The fourth myth, “Globalization is good for the environment” is 
            simply shattered, with Roddick using George W. Bush’s quote - “It 
            isn’t pollution that’s harming the environment. It’s the impurities 
            in our air and water that are doing it.” Most readers will get a 
            gist of what follows in the chapter instantly with his 
            not-so-sagacious words! <br>
            <br>
            This absolute disregard of our environment by someone at the helm of 
            affairs of a superpower is unheard of. However, it could have been a 
            “slip of the tongue” as well. One should give the not-so-popular 
            President the benefit of doubt. But as the reader goes on flipping 
            through the pages, it becomes increasingly difficult not to believe 
            what the leading activists and environmentalists from across the 
            world have to say on the issue. <br>
            <br>
            As Maude Barlow says: “There is simply no way to overstate the water 
            crisis of the planet today. Many now predict that the wars of this 
            century will be fought over water.” <br>
            <br>
            Andrew Simms has written an enlightening paper on how the developed 
            world owes an invisible ‘carbon debt’ for all the pollution it 
            continues to generate. The book is ‘loaded’ to describe it in brief. 
            The articles from ‘Greenpeace’ will certainly not let you eat or 
            breathe in peace any more. Even meditation may become difficult. Be 
            warned! Too much carbon dioxide in the air, they warn. <br>
            <br>
            The last myth, “Economic globalization is inevitable” is examined as 
            just an eyewash by people who stand to gain from that slogan. The 
            author feels it’s only “designed to make everyone feel nothing can 
            be done about it, thus promoting passivity.” She also deals a blow 
            to economists – “… the prevailing official world view is that the 
            economists have barely any hands at all…” She urges them to take a 
            stand, and indeed everyone else as well, by getting connected with 
            their conscience and using their money wisely. It’s a thought 
            provoking and cleverly written book. Are the corporate giants and 
            globalization advocates listening?<i>- Uma Devi Jadhav</i></font></p>
