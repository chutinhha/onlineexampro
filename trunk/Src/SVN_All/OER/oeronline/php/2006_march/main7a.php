</font><b><font face="Verdana" size="5">Making of a great leadership</font></b></p>
<p align="justify" dir="ltr" style="margin-right: 10px">
<font face="Verdana" size="1">
<b>Prof. Kakabadse of UK’s Cranfield University talks to OER on a host of issues 
that are critical to leadership</b><br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>
<img border="0" src="manag2.jpg" width="100" height="99" align="left" hspace="5" vspace="2">I</b></font><font face="Verdana" size="1">n 
today’s dynamic global market, the leaders are made in boardrooms but great 
leadership is made in workshops conducted by management gurus. In February, 
College of Banking &amp; Financial Studies in association with Central Bank of Oman 
organized “Visioning Workshop – Attributes of World Class Leaders” in Muscat for 
the benefit of leaders in private and public sector organisations. It was 
conducted by Prof. Andrew P Kakabadse, International Management Development, 
Cranfield University, UK. Oman Economic Review was the official media partner 
for the event.<br>
<br>
Attended by the senior managers drawn from a cross section of private sector and 
public sector organisations, the workshop was based on a model that has emerged 
from research, done by Cranfield University, of the key steps to generating 
visions that work. HE Hamoud Sangour Al Zadjali, Executive President, Central 
Bank of Oman, commenting on the workshop said, “Given the current trend on the 
importance of leadership, the Central Bank of Oman and the College view our role 
in leadership development as a strategic move. This current emphasis on 
leadership programmes is essential for the continued and strong economic 
development of Oman.”<br>
<br>
Prof. Andrew’s current areas of interest focus on improving the performance of 
top executives and top executive teams, excellence in consultancy practice, 
corporate governance and conflict resolution and international relations. His 
top team database covers 17 countries and many thousands of private and public 
sector organizations. <br>
<br>
The study of the strategic skills of top teams has now extended into Japan, 
China, Hong Kong and the USA. He is about to embark on a major world study of 
boardroom effectiveness and governance practice. <br>
<br>
Besides his future course of action in Oman, Prof. Andrew talked at length with 
Akshay Bhatnagar on the leadership challenges ahead and what goes into the 
making of a great leader. Excerpts of the talk:<br>
<b><br>
What was the objective of the workshop?</b><br>
The top managers in leadership role hold accurate and powerful insights 
concerning the strategic, operational and by implication, the leadership 
challenges facing their organisation. The concern is whether such insights can 
be ‘raised to the surface’ and meaningfully discussed. The workshop assisted 
each of the participants to appreciate the nature of the challenges they face in 
their ‘real life’ work related context. It also aimed at supporting the 
development of the necessary leadership capabilities to enhance their ability to 
address present and future concerns.<br>
<b><br>
What other activities you are planning in the region?</b><br>
I want to introduce more research-related activities in the region, especially 
in Oman. There are no Gulf-wide research benchmarks. <br>
I would say that there are cultural and economic disparities within the region 
but that’s what makes the task more challenging to us. The international 
business environment is also equally, if not more, diverse in nature. <br>
<b><br>
…but not many organizations and government bodies in the region are willing to 
part with the data they have?</b><br>
This is true. We have to be sensitive in doing our research. We need to develop 
better relationships within the region. This kind of a hindrance is not unique 
to the region alone. There are other markets, like China, which are far more 
stringent and cagey in sharing the data. <br>
<br>
It’s a matter of developing an understanding in the market and influencing the 
right people in the benefits of research.<br>
