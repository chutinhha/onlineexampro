<p align="left" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><font face="Verdana" style="font-size: 16pt"><b>‘Stronger IPR protection will help 
Omani companies’</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt"><b>
<img border="0" src="infouc.jpg" width="100" height="105" align="left" hspace="5" vspace="2">W</b></font><font face="Verdana" size="1">ith 
the signing of the US-Oman FTA, issues concerning intellectual property rights 
(IPR) in the Sultanate come to the forefront once again. Mehre Alam caught up 
with Peter N. Fowler, Senior Counsel, Office of Enforcement, Department of 
Commerce of the US Government, while the latter was on a visit to the Sultanate 
recently. Fowler spoke at length on some of the key issues involving IPR in Oman 
in particular and the region in general. Excerpts:<br>
<b><br>
<br>
What impact will the US-Oman FTA have on the IPR scene in Oman?</b><br>
I think the FTA will have an impact. There will be heightened requirements for 
changes and revisions in the laws that now have to comply with the FTA. <b><br>
<br>
What are these new requirements?</b><br>
First, create a higher standard of protection for intellectual property in Oman, 
which is somewhat unique in the region. Secondly, also create new procedures and 
a higher level of enforcement. Now, Oman is set to experience higher trade with 
the US and from other countries as well, in effect because of the FTA with the 
US. Unfortunately, we’ll also see an increase in counterfeit products coming in 
from other countries and other regions. <br>
<br>
So, Oman will have to put a greater emphasis on border and customs enforcement. 
Besides, there is a public interest in seeing stronger IP enforcement. This is a 
consumer protection issue after all – that products are safe, they do not 
represent any risk, harm or injury to individuals, and that the products are not 
defective. There is a definite overlap in terms of good IP enforcement and good 
consumer protection, public health and safety.<br>
<b><br>
In what sense is the IPR situation in Oman unique?</b><br>
The standards for IP protection set under the FTA are very high. These standards 
are higher than those under the WTO TRIPS Agreement. And it’s good because a 
number of countries in the region are negotiating FTA with the US. This will set 
a higher standard of protection, which, in turn, will attract hi-tech investment 
- hi-tech industries and any industry or business that relies on intellectual 
property assets like those involved in research and scientific development. And 
it’s also good because these are the kinds of industries and jobs that Oman 
wants to attract.<br>
<b><br>
Do you see this region slightly lagging behind when it comes to IPR awareness?
</b><br>
Yes. In some respects, intellectual property protection is a new concept in this 
region although not a completely new one. It is somewhat new concept 
particularly in the area of copyright and patent protection. <br>
So, there is an element of public education and public information. The 
government does need to convey the importance of IP protection and enforcement 
to its citizens. I think companies that own IP assets also need to be involved 
in the process. There is a need to increase awareness about IPR’s importance not 
only to the companies as businesses but also to the society – to developing jobs 
and attracting investments and what it really means to Oman as a country. <br>
<b><br>
How you plan to help the Oman government in framing the relevant IPR laws?</b><br>
We are trying to reach public officials through seminars and other training 
programmes and capacity building programmes to expose them to the issues around 
IP protection and enforcement. <br>
<br>
In addition, those of us in our office and others in the US government are 
readily available to provide technical assistance to Oman as it goes to the 
process of drafting new regulations and revisions to decrees to comply with and 
implement the FTA obligations. So, it’s not just a simple matter of a country 
having signed the FTA and it’s over. <br>
<br>
We’ll be there to assist in every single way, both in terms of human capacity 
building as well as institutional, legislative and regulatory capacity building.<br>
<b><br>
How soon should we expect this to happen?</b><br>
It’s already been happening. We have been working with Oman. The US government 
had been working with the Omani government for the last couple of years in 
anticipation of the FTA. There has been a lot of technical assistance. <br>
<br>
The FTA is still some months from its implementation, as it needs to be passed 
by the respective legislatures of the two countries. What are you doing to 
prepare the groundwork?<br>
<br>
The Patent and Trademark Office (PTO), US, as well as other agencies are working 
with the Omani government to identify various areas for technical assistance 
where they think they require training and help. We were not going to come in 
and tell them what to do or where they needed to do things. The agreement is 
fairly clear on that. But we stand ready to help them in any way possible, 
whether it’s revising laws, revising decrees, human capacity building or 
training process. This is an ongoing, regular process.<br>
<b><br>
Do you nurture any particular area/s of concern? </b><br>
I think one area of concern is in the area of copyright infringement. Oman has 
certainly improved the situation over the last few years. It’s much better than 
what it used to be in terms of pirated DVDs and pirated copyright materials. But 
they (pirated CDs, DVDs etc.) still circulate. You can go to a lot of places to 
buy pirated DVDs or pirated software. It’s still available, and rather 
substantially, at some places. <br>
<br>
I think as more people gain access to internet and have greater broadband 
capacity to download copyrighted materials from the internet, this will be a 
continuing problem and a greater challenge. Internet copyright piracy is a 
challenge for law enforcement in terms of resources and the technical ability to 
handle it and prosecute such types of crimes. These are the issues that the 
Omani government can certainly deal with. But this is a challenge that every 
country faces and not just Oman. The US has a challenge in this respect because 
the technology makes it so easy and so available. <br>
<b><br>
In what ways will the FTA benefit Oman’s economy vis-à-vis greater IPR 
protection?</b><br>
I think the major impact – in the area of intellectual property from the FTA – 
will be on the perception front. Better laws and better law enforcement will act 
as a seal of approval for many companies looking to invest or bring their 
operations into the region. It will attract companies that are looking for 
strong IP protection. And, Oman will be seen as a genuine destination for such 
companies and businesses. <br>
<br>
In addition, the increased protection will certainly benefit the local Omani 
companies, which rely, or need to rely, on intellectual property protection for 
business development – whether it’s local software developers, pharmaceutical 
researchers or businesses that have a successful brand name. If the local 
companies or businesses rely on intellectual property, then a stronger framework 
of intellectual property, stronger environment and enforcement will benefit them 
as well.
</font></p>

