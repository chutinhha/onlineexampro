                            offer</b><br>
                            Alliance Housing Bank has launched a limited period 
                            loan offering a three year fixed interest of 6.99 
                            per cent without any concern over fluctuation in the 
                            interest rates. The loans can be taken to buy or 
                            build a house, buy land or refinance existing 
                            property loans. <br>
                            Karim Boukarroum, Head of Marketing, AHB said, “It 
                            really is an attractive proposition for customers 
                            who are looking for housing finance. For the first 
                            time in the Sultanate customers can take up to 90 
                            per cent of the property’s value in loans which are 
                            payable over a period of up to 25 years.”</font><hr color="#C0C0C0" width="80%" size="1">
