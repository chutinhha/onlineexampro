                  </b><font face="Verdana" size="1"><b>
BMW gets 
more affordable</b><br>
<b>
<img border="0" src="auton1.jpg" width="150" height="81" align="right"></b>Al Jenaibi International Automobiles, the exclusive BMW Importer in Oman, is 
offering ‘BMW Service + Repair Inclusive’ agreement to new BMW car buyers. The 
offer includes three-year/60,000 km service plus repairs inclusive agreement as 
a standard feature. It covers servicing and maintenance work, oil changes, 
labour charges, and wear-and-tear repairs with original BMW parts. “This 
programme is a BMW Group approved international programme designed to unify the 
ownership experience across the world. The customers can now enhance the 
residual value of their BMW cars and avoid service, maintenance and repair costs 
with this creative and flexible programme,” said Vishal Dhawan, MD, Al Jenaibi 
International Automobiles.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>Towell 
                    Auto Centre gets Bridgestone award</b><br>
                    In a competition held by Bridgestone recently in Japan, 
                    Towell Auto Centre stood first among all the other 
                    Bridgestone distributors in the GCC region. Keeping in line 
                    with its concept of “closer to customers”, Towell Auto 
                    Centre has set up 12 state-of-the-art ‘Tyre Care Plus’ 
                    outlets in different parts of Oman to provide sales and 
                    service to the customers.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>Nissan 
                    to launch 19 new models</b><br>
                    Nissan has revealed its ambitious new expansion plans for 
                    its global and Middle East operations. Talking from the 
                    sidelines of Dubai International Motor Show, Nissan 
                    spokesperson informed that the auto giant plans to launch 19 
                    new models in the next three years. The annual unit sales 
                    are expected to rise to 4.2 million. In the GCC alone, 40 
                    per cent sales growth is targeted for the three-year period, 
                    bringing annual sales to more than 140,000 and giving Nissan 
                    30 per cent of the Gulf market. In Oman, Suhail Bahwan 
                    Automobiles LLC backs the Nissan brand.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="auton2.jpg" width="200" height="120" align="left">The new Mercedes-Benz M-Class has been voted as the “Best 
                    Sports Utility Vehicle” by Autocar Middle East. M-Class was 
                    applauded for its class leading features, dynamic 
                    performance and highest levels of comfort. The new M-Class 
                    is equipped with the world’s only seven-speed automatic 
                    transmission, which is operated electronically using a 
                    selector level positioned close to the steering wheel. The 
                    engine range for the new M-Class includes the proven 
                    eight-cylinder unit at the heart of the ML 500 which 
                    delivers 306 hp, while the new V6 petrol unit powering the 
                    ML 350 delivers 272 hp.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>
                    New 
                    Kia “cee’d” concept car </b><br>
                    <b>
                    <img border="0" src="auton3.jpg" align="right"></b>Kia Motors is expected to unveil an exciting new concept car 
                    at the 76th Salon de l’Automobiles, in Geneva, Switzerland. 
                    The attractive and styled show car, named cee’d (pronounced 
                    ‘seed’), is based on the forthcoming all-new C-segment model 
                    which Kia will begin building at its first-ever European 
                    assembly plant in Slovakia from December 2006. With its 
                    tough appearance, combining strongly solid forms with 
                    sensitively flowing lines, jewel-like detailing and a broad 
                    stance, cee’d points towards the future elements of Kia’s 
                    evolving design language. “Our design brief was to create a 
                    ‘teaser’ for the all-new Kia which has been designed 
                    entirely in Europe and will make its world premiere at the 
                    Paris Show in September,” explained Gregory Guillaume, Chief 
                    Designer Kia, European Design Centre.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>
                    <img border="0" src="auton4.jpg" width="200" height="99" align="left">Audi’s 
                    ME quattro tour</b><br>
                    Audi Middle East is celebrating the 25th anniversary of 
                    quattro technology with a test-drive tour around the Middle 
                    East region. The Audi brand truck will be visiting Dubai, 
                    Muscat, Kuwait City and Jeddah besides other places. Audi 
                    was the first car manufacturer to introduce a permanent 
                    four-wheel drive specifically tailored to high performance 
                    on-road passenger cars. The pioneering technology was named 
                    “quattro”.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>
                    <img border="0" src="auton5.jpg" width="150" height="99" align="right">Renault’s 
                    Formula1 car in Oman</b><br>
                    Suhail Bahwan Automobiles LLC, the authorised dealer of 
                    Renault vehicles in Oman, has brought the Formula 1 
                    experience closer to home. On display at the recently 
                    inaugurated Renault showroom at Qurum are masterpieces from 
                    the Formula 1 stable of Renault; the Renault powered Formula 
                    1 car that drove Fernando Alonso to his first Driver's 
                    Championship and Constructors Championship in 2005.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>
                    <img border="0" src="auton6.jpg" width="150" height="98" align="left">Skoda 
                    Auto sales up by 9 per cent</b><br>
                    Skoda Auto delivered a total of 492,111 cars to its 
                    customers in almost 90 worldwide markets to clock a growth 
                    of 9 per cent in 2005. The best-selling model range was the 
                    Fabia closely followed by the Octavia model family. The most 
                    successful markets were Germany, Great Britain, and Spain 
                    where Skoda also markedly strengthened its market share and 
                    registered new sales records.</font></p><hr width="80%" size="1">

                    <p align="justify"><font face="Verdana" size="1"><b>
                    Volkswagen Concept A</b><br>
                    <b>
                    <img border="0" src="auton7.jpg" width="150" height="104" align="right"></b>Volkswagen 
                    calls the Concept A “the child of a new era.” The concept 
                    has features like all-wheel drive, suicide rear half-doors, 
                    huge wheels, and styling that blurs the line between SUV and 
                    sports car. But unlike countless other coupe-truck creators, 
                    Volkswagen will be putting a vehicle similar to the Concept 
                    A into production within the next few years. The new model 
                    will most likely be based off the Golf platform and will fit 
                    in the VW line-up under the Touareg. The Concept A uses the 
                    new 1.5-liter Twincharger engine, but the upcoming model, 
                    possibly called the Marrakesh, will also put VWs lovely 
                    2.0-liter FSI Turbo and a diesel to use. This is a 
                    cool-looking truck, and we are excited to see the final 
                    product when it comes on the scene in 2007.</font></p>

