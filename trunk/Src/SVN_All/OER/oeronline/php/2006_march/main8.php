</font><b><font face="Verdana" size="5">New peaks round the corner?</font></b></p>
<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 8pt">
Corporate sector registers a marked improvement</font></b><font face="Verdana" size="1"><br>
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>T</b></font><font face="Verdana" size="1">he 
stock market has corrected itself during this period after a substantial rise 
last month. This correction is healthy for the market. We find that the bulls 
are still holding the charge and there are very high probabilities for the 
market to cross the previous peak of 5,700 in the next 2 to 4 months time. <br>
<br>
With the government spending huge amounts on the infrastructure and development, 
the economy will accelerate at a faster pace, which in turn will propel the 
growth of the industrial sector. The external economic environment also holds 
out a lot of promise for the domestic economy thanks to the continuous increase 
in crude oil prices. <br>
<b><br>
Cement: promising sector</b><br>
Among the various sectors, we find that cement companies hold out a lot of 
promise as there will be huge demand emanating from the construction activity. 
Besides cement, one can also look into the steel sector. International steel 
prices took a severe beating between April and December last year. Now, there 
are indications that steel prices are bottoming out at this level. A rise in the 
international prices will create huge opportunities for companies operating in 
the steel sector.<br>
<br>
It is imperative to also highlight the positive developments taking place in the 
other sectors in Muscat Securities Market. The banking sector holds out a great 
promise. Banking and investment play a key role when an economy is on course of 
a sustained path of development. The services sector, though defensive in 
nature, is also likely to register higher growth rates when the economy expands.<br>
<br>
The corporate results for Q4-2005 are exceptionally good. To cite certain 
examples,<br>
• National Bank of Oman has reported a 289 per cent increase in profits compared 
to 2004 and declared a cash dividend of 15 per cent. <br>
• Bank Muscat reported a 33 per cent increase in profits compared to 2004 and 
declared a cash dividend of 35 per cent and a stock dividend of 10 per cent.<br>
• Oman International Bank reported a 56 per cent increase in profits compared to 
2004 and declared a cash dividend of 23 per cent and a stock dividend of 20 per 
cent.<br>
• Oman Cement reported a 38 per cent increase in profits compared to 2004 and 
declared a cash dividend of 25 per cent. <br>
• Raysut Cement reported a 45 per cent increase in profits compared to 2004 and 
declared a cash dividend of 25 per cent. <br>
• Ominvest reported a 40 per cent increase in profits compared to 2004 and 
declared a cash dividend of 40 per cent .<br>
• Alliance Housing Bank reported a 24 per cent increase in profits compared to 
2004 and declared a cash dividend of 20 per cent.<br>
• Oman Oil Marketing reported a 38 per cent increase in profits compared to 2004 
and declared a cash dividend of 45 per cent.<br>
• Shell Oman reported a 41 per cent increase in profits compared to 2004 and 
declared a cash dividend of 75 per cent.<br>
<b><br>
Twin Signals</b><br>
We find that there is an improvement in the performance of corporate sector 
across the board. This indicates two things. One, the promising external 
environment that the government has created by investing more and more in 
infrastructure and development. Two, the qualitative improvement in the 
managerial capabilities across the spectrum of various industries, which is able 
to effectively capitalize on the advantages in the external environment.<br>
<br>
An increase in international crude oil prices, an accelerated investment by the 
government on the domestic economy, an improved corporate performance, a well 
regulated stock market with rigorous reporting standards imposed by Capital 
Market Authority and an attractive valuation of the MSM at a PE ratio of 13 
compared to 35+ PE ratios in most of the GCC countries, all point towards 
excellent days ahead for the economy and stock market. <br>
<br>
Investors will stand to benefit a lot if they stay invested in good companies 
with sound management and decent track record in business.</font></p><table border="0" cellpadding="0" style="border-collapse: collapse" width="100%" id="table2">
