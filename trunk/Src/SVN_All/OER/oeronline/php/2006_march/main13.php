<p align="justify" dir="ltr"><font face="Verdana" size="1">
<br>
<br>
</font><b><font face="Verdana" size="5">A lot to rave about</font></b><font face="Verdana" size="1"><br>
<b><br>
Toyota’s small and sporty RAV4 has just got bigger, more spacious and more 
powerful. Anne Kurian test-drives the all-new 2006 RAV4</b><br>
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>
<img border="0" src="cart.jpg" width="200" height="115" align="left" hspace="5" vspace="2">M</b></font><font face="Verdana" size="1">ore 
than a decade after Toyota launched the RAV4 (Recreational Activity Vehicle 
4WD), the automobile giant has re-introduced the 4WD in a whole new packaging. A 
bigger, solid body has replaced the cute, boxy appearance of the RAV4, while the 
interiors have undergone a facelift too.<br>
<br>
If the earlier model was more of a fun vehicle for singletons and couples, the 
2006 model is a family vehicle with its sporty features intact. To top it, there 
are three different versions in the new RAV4 to choose from. <br>
<br>
The starting range RAV4 Special comes with a manual as well as automatic 
transmissions, whereas the next two categories - Premium and Exclusive - have 
only automatic transmissions and, naturally therefore, more features too! <br>
<br>
Though the remodeled RAV4 does not compete in the category of a hardcore SUV, 
the new model does not lack in top end features and characteristics that places 
it in the forefront when compared to its competitors. Styling has been given 
serious thought, which shows in RAV4's lesser curves, brawny exterior and a 
whole new finished appearance. As for the performance, the 2.4-litre engine 
shows off the new RAV4's power on the tarmac. <br>
<b><br>
The Exterior Factor</b><br>
The new RAV4 is bigger and longer than its predecessor, which takes away the 
cute factor it had earlier. Instead, what you have now is a more brawny and 
solid frame that accentuates the appearance of the new model -- taking it a 
notch higher in styling and symmetry. The new RAV4 has a wider track that gives 
it a more compact stance and fewer chances of rolling over during risky or 
sudden turns. Once you get behind the wheels of the new model, it's hard not to 
try out a few quick maneuvers and turns; the wider stance is blessing in 
disguise. <br>
<br>
The headlamps seem to have got more compact and all the more powerful as we 
found out on the dimly lit roads of Wattayah. The taillights and stop lamps come 
wth LEDs that ensure better visibility for following cars. And if that's not 
enough, a high-mount LED brake lamp has also been incorporated in the new model.
<br>
<br>
If durability concerns you, then this bit of information will surely rake up 
your interest level. The new RAV4 boasts of anti-chip coating and 
rust-prevention wax on the body. <br>
<b><br>
The Interior Factor</b><br>
The bigger on the outside, the more spacious it gets inside. And the new RAV4 
does not prove us wrong. Space is no constraint inside the new model, be it for 
passengers or storage. Leg space inside the 2006 model has increased and five 
passengers can comfortably travel in the RAV4, unlike in the earlier version, 
where four was a tight squeeze. <br>
The storage space is impressive too with cubbyholes and compartments positioned 
strategically inside the vehicle. For larger luggage storage, the rear space is 
said to comfortably hold four large Delsey suitcases even with the backseats 
upright. This means in case the crowd gets bigger, you know where to fit in the 
extra ‘load’!<br>
<br>
The controls are more or less similar to the earlier version, though there are a 
few modifications. One such change that impressed us was the positioning of the 
side mirror controls. Unlike in the regular sedans and some SUV models, the 
controls for the side mirrors are on the center console, right next to the hand 
brake. Now, this is quite handy as there's very little distraction or stretching 
to do while adjusting the rear view mirrors. We took the RAV4 Premium for the 
road test and this model is equipped with individual air conditioner controls.
<br>
<b><br>
On Road</b><br>
The 2.4-litre engine has remarkable power to throttle along the highways 
smoothly and nimbly. Ascending a fairly steep road did not falter the RAV4 even 
for a second. In fact, I had to apply a bit of caution not to overdo on the 
accelerator, for the RAV4 pulled along like a pro. <br>
<br>
The RAV4 is quite easy to maneuver on and off the roads, though it's not exactly 
an off-road vehicle. The road grip is impressive and quick curves or turns do 
not seem to take the RAV4 by surprise. <br>
<br>
The new version does come with Down Hill Assist Control and Hill Start Assist 
Control (the latter features only in the Exclusive model), but then, it does not 
exactly fit into the rough and tough category of vehicles and would be more 
suited to the less mountainous regions of Oman. But that does decrease the fun 
element of the RAV4, for you can rely on it to take you quite longer and higher 
distances than the earlier version did.<br>
<b><br>
Safety and Security</b><br>
Even at the risk of reading like a vehicle manual, it has to be said that Toyota 
vehicles come with the stamp of reliability and safety. The new RAV4 only 
strengthens this aspect. Safety factors like reduction of pedestrian injury in 
case of a pedestrian collision, whiplash injury-lessening concept, seats to 
minimize head injury and stronger and energy-absorbent body that takes on 
minimum impact during a crash, have all been introduced in the RAV4. <br>
The RAV4 is also equipped with additional safety features like airbags for the 
front passengers and side airbags for front and rear seat passengers. And for 
added security, Toyota has also installed an anti-theft engine immoboliser 
system, which is one of its high-end features.<br>
<br>
To cut a long story short, the new RAV4 not only scores in the appearance 
section, but also provides a whole new driving experience. Toyota has managed to 
balance the two aspects that interest many a driver today - serious driving 
quality without doing away with the fun aspect! <i><br>
<br>
Road test courtesy Saud Bahwan Automotive LLC, the sole dealers of Toyota 
vehicles in Oman</i></font></p>
