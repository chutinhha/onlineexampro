</font><b><font face="Verdana" size="5">Woods vs Nicklaus</font></b><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="3"><b>
<br>
</b></font><font face="Verdana" style="font-size: 14pt"><b>W</b></font><font face="Verdana" size="1">oods 
is Nicklaus; Nicklaus is Woods. This is the grand comparison of men's 
professional golf, of course, and has been for most of the past decade or ever 
since Woods began construction of his own legend in pursuit of Nicklaus' 
greatest records. Ah, yes, the game's major championships. Those are the sport's 
touchstones, and have provided the most convenient — and, yes, surely, the most 
important — measure of the two men as players.<br>
<br>
&quot;Sounds about right,&quot; Woods said. It has sounded about right to Woods because 
the story goes that he has been tracking Nicklaus' path from the time he pinned 
a chart on a bedroom wall. Now, it sounds about right to everyone simply because 
Woods has played himself into a role as a legitimate challenger to Nicklaus' 
mark of 18 major professional titles.<br>
<br>
&quot;How can you miss it?&quot; Nicklaus joked. You can't. Woods won 10 majors — four 
Masters and two each of the U.S. Open, British Open and PGA Championship 
contests— while still in his 20s to beat Nicklaus' pace at the same age. 
Nicklaus won seven major titles (three Masters, two U.S. Opens, one British Open 
and one PGA Championship) in his 20s, though he did get a later PGA Tour start 
than Woods by a couple of years.<br>
<br>
But the comparison goes deeper than that. It's also an interesting study to see 
how much the same Woods and Nicklaus were in overall performance through their 
20s. Nicklaus stayed a little busier on per year average of tournaments played, 
and Woods won a little more often on same per year average. But the variations 
are slight. Hardly ever did either miss a cut. They had similar numbers for 
collecting second- and third-place paychecks, and their percentages of finishing 
in the top 10 and top 25 are startlingly close.<br>
<br>
<img border="0" src="golf.jpg" width="100" height="88" align="left">&quot;That can't 
be,&quot; Nicklaus said. &quot;Tiger wins all the time. It seems like he does, anyway.&quot; 
But it seemed that way, too, during the Nicklaus heyday. Now, however, Woods' 
recent 30th birthday provides another milestone marker in comparison to 
Nicklaus' career. &quot;He'll be a better player than he has been,&quot; Nicklaus said. &quot;I 
know I was. I hit my driver better. I had a better short game. I was more calm. 
I was more mature.<br>
<br>
Nicklaus was ridiculously good in his 30s. He won eight majors (three more came 
in his 40s), and his overall standard of play remained as superb as it had been 
in his 20s. Is it possible Woods, who says the game is &quot;easier&quot; now, can 
duplicate his magnificent 20s during the upcoming decade?<br>
<br>
<img border="0" src="golf1.jpg" width="100" height="124" align="right">&quot;That's ultimately what you want to happen,&quot; Woods said. &quot;This game is fluid. I'm understanding the game so much better. When I first came out here, I didn't 
really understand how to put together a round. I'd just go out and hit it and 
try to make some putts. To manage the game takes time to learn. It's something 
that has happened through the maturation process.<br>
<br>
&quot;I've been out here since I was 20... two totally different deals. I'm looking 
forward to some of the best years being ahead of me.&quot; That's how it worked for 
Nicklaus, who even today thinks golfers are in career prime in their early 30s. 
Nicklaus won seven major titles between the ages of 30 and 35 and otherwise 
dominated the PGA Tour as well. Woods wasn't surprised by Nicklaus' point of 
view. &quot;That happens with a lot of athletes,&quot; Woods said. &quot;You get to be 32 or 
35, and your mind and body come together.<br>
<br>
&quot;Life is not fun unless you're competing and trying to do something.&quot; And the 
long-term golf competition lies in Eldrick as Jack... in Tiger as the Golden 
Bear... in Woods as Nicklaus. &quot;I've done what I can do,&quot; Nicklaus said. &quot;Do I 
wish I had more (majors)? When he gets to 18, I probably will.&quot;<br>
<br>
Nicklaus says he's &quot;intrigued&quot; by Woods, and flattered by any comparison. It is 
a older man's perspective on the wonder of his own younger years being played 
out by someone else.<br>
<br>
Woods' next decade awaits.- by Greg Stoda. Content courtesy Palm Beach Post.</font></p>

