<p align="justify" dir="ltr"><font face="Verdana" size="1">
&nbsp;<i>Source: HotelBenchmark™ Survey by Deloittes</i><br>
<br>
Lebanon was the poorest performing country during 2005 as visitor arrivals fell 
11 per cent during the first 11 months of the year following the car bomb that 
killed former Prime Minister Rafiq in February. As visitors shied away from the 
country occupancy in the capital Beirut crashed 29 per cent to reach 48 per cent 
for the year. Egypt too suffered from terrorism attacks in Cairo and Sharm 
el-Sheikh which contributed to a fall in demand. In Cairo occupancy fell 4 per 
cent whilst in Hurghada and Sharm el-Sheikh occupancy tumbled 14 per cent and 12 
per cent respectively. Encouragingly however occupancy in Luxor grew 22 per cent 
to reach 56 per cent and this combined with a 13 per cent increase in average 
room rates helped revPAR across the country move ahead 5.1 per cent.<br>
<br>
Amman is the latest market to have been affected by terrorism following the 
detonation of three bombs in prominent hotels on 9 November 2005. In December 
occupancy tumbled 32 per cent, however with the attacks occurring so late in the 
year the overall impact on the annualised results was minimal. We wait to see 
how performance will be affected during the first quarter of 2006. <br>
<br>
<img border="0" src="burj_al_dubai.jpg" align="right">Conversely, occupancy levels across the UAE continue to exceed the 80 per cent 
barrier with hotels in Dubai reporting annualised occupancy levels of 85 per 
cent. Across the Emirate there is strong all year demand with many months 
reporting demand in excess of 90 per cent. This contrasts with hotels in Saudi 
Arabia which have the most volatile demand patterns. Makkah and Medina in 
particular are hajj (pilgrimage) destinations and therefore have a very seasonal 
performance. At the height of the pilgrim season occupancy can reach 90 per cent 
but during the off-season demand falls away resulting in occupancy levels below 
20 per cent.<br>
<i>Visual – Burj al dubai</i><br>
<br>
<img border="0" src="egypt.jpg" width="200" height="150" align="left">In terms of average room rate Kuwait remains the most expensive country with 
average room rates currently trading at US$196. Compare this to hotels in Egypt 
where the average room rate is nearly 400 per cent lower at just US$57. 
Therefore it is hardly surprising that Egypt is ranked one of the most price 
competitive destinations by the World Travel and Tourism Council. <br>
<br>
Commenting on the results, Julia Felton, Executive Director of the 
HotelBenchmark™ at Deloitte said “The Middle East has undergone a metamorphosis 
during the last decade, so naturally hotel performance has benefited. 
International visitor arrivals to the Middle East have nearly tripled from 13.5 
million in 1995 to 38.4 million in 2005, representing a compound annual growth 
rate of 12.3 per cent.”<br>
<br>
“Increased demand has been supported by the transformation of both the airline 
and hotel industry across the region. Passenger traffic through the Gulf is the 
now fastest growing in the world, rising by 10 per cent per annum and there has 
been a rapid expansion of regionally-based international carriers such as 
Emirates Airlines, Eithad Airways, Qatar Airways and Gulf Air. Hotel development 
continues unabated across the regions with all the global hotel brands either 
having established or have advanced plans to expand their significant presence 
in the region. It is estimated that 80 new hotels will open on the Arabian 
Peninsula by 2008. Dubai leads the field and remains the major focus of 
development with an estimated 12,500 rooms being added to supply over the next 
four years. Qatar and Kuwait are also projected to see increases in rooms’ 
supply of 145 per cent and 75 per cent respectively.”<br>
<br>
“Our prognosis is that the future continues to look bright for the hotel sector 
in the Middle East and the development of more mid-scale and budget 
accommodation should help fuel growth further in years to come.”<i><br>
