<p align="justify" dir="ltr"><font face="Verdana" size="2" color="#FF3300">
<br>
<b>Web exclusive/ME Hotel Report</b></font><font face="Verdana" size="1"><br>
<b>
<br>
Over 70% hotel occupancy in Oman </b> <br>
With an impressive 21 per cent growth in revenue per available room (revPAR), 
the Middle East region was, for the second year in a row, the best performing 
region in the world during 2005 according to the HotelBenchmark™ Survey by 
Deloitte. The growth was fuelled by an incredible 23 per cent increase in 
average room rates which soared to US$117 compared to US$95 in 2004. Despite 
falling back slightly occupancy continues to be at an all time high at nearly 70 
per cent. This contrasts starkly with the 60 per cent occupancy levels reported 
in wake of the 9/11 attacks and subsequent wars in both Afghanistan and Iraq.<br>
<br>
With over 60 per cent of markets in the Middle East reporting double-digit 
revPAR increases, it is hardly surprising that the region took the gold medal 
for hotel performance growth. Of all the 165 markets tracked by the 
HotelBenchmark™ Global Ranking Index, the Middle East secured six of the top ten 
spots in terms of revPAR growth in 2005. Hotels in Qatar won the coveted first 
prize as revPAR stormed ahead 62 per cent whilst hotels in Oman took third prize 
with revPAR growth of 54 per cent. Abu Dhabi and Riyadh also featured in the top 
ten along with the Israeli cities of Jerusalem and Tel Aviv.<br>
<br>
The impressive growth across the region is all the more remarkable given the 
increasing terrorist activity during 2005 which has hindered the performance of 
some markets, whilst bolstering others. As a result there is a marked disparity 
in hotel occupancy and average room rates, with some countries performing way 
above the average and some way below.
