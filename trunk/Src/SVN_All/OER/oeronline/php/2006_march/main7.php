<p align="justify" dir="ltr" style="margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" style="font-size: 18pt">
<b>Tugs of conscience and commerce</b></font></p>
<p align="justify" dir="ltr" style="margin-right: 10px">
<font face="Verdana" style="font-size: 14pt">
<b>
<img border="1" src="manag.jpg" width="83" height="126" align="left" hspace="5">I</b></font><font face="Verdana" size="1">
am inspired by the out-of-the-box experiments in enterprise that proliferate 
during our moment. The social venture initiative, led by entrepreneurs who 
launch for-profit companies to solve a human problem, has turned into a 
movement. Meanwhile, not-for-profit organizations are becoming increasingly 
savvy about deploying business practices to set their projects free from the 
unending cycles of charitable support. We can see emerging a generation of 
leaders who can walk comfortably in both worlds without contradiction. <br>
<br>
In fact, Michael Kieschnick, President of Working Assets - a US $140 million 
annual revenue telephone company with an advocacy agenda - teaches a course at 
Stanford University in this vein. Michael helps students define the best 
organizational vehicle for a given social mission; sometimes a not-for-profit 
better enables social change, but not always. What matters most is, 
understanding the organizational structure as a tool for accomplishing a 
purpose.<br>
<br>
The devil is in the details of implementation, of course, and social 
entrepreneurs face considerable obstacles. Suffice it to say that not evryone 
operating in the financial markets has jumped on board the social venture train. 
Finding like-minded investors is the first tall wall they have to figure out how 
to scale. That's no easy task, for the majority of investors still think 
somewhat conventionally about profit and philanthropy.<br>
<br>
<img border="0" src="manag1.jpg" width="100" height="144" align="left">If they want to do some good in the world, most investors will donate funds to a 
philanthropic group that targets issues close to their hearts. It is very 
&quot;clean&quot; transaction: they donate funds, take the tax write-off, and do not 
expect anything back in return. So it's complicated for a social venture 
entrepreneur to make an investment pitch that blurs these categories. The 
investor knows how to evaluate return on investment for a strictly commercial 
deal. Likewise, the investor knows how to evaluate philanthropic giving. But the 
social venture deal takes the investor into uncharted waters, and in most cases 
he or she is not comfortable sailing there.<br>
<br>
John Sage has done some of the most creative thinking around investment models 
that I have run across. I spent a morning in Seattle with John recently and our 
conversation inspired this column. A former marketing whiz at Microsoft, John 
launched seven years ago Pura Vida Coffee, a social venture enterprise in the 
coffee industry. <br>
<br>
In one effort to surmount the commerce and philanthropy divide, John gave 
potential investors the opportunity to invest in his for-profit venture, but 
designate the return on that investment to the investor's favorite charitable 
organization. In essence, John was offering a hybrid option as a way of dealing 
with what was a muddled business model in the mind of the investor.<br>
<br>
Though the model did have a modicum of success, John has not relied on it for 
his bread-and-butter investments. He experimented with a number of other hybrid 
offerings, but pretty much has fallen back to appealing to those investors who 
are willing to make a straight investment in his company simply because they 
believe in both the business and the social mission.<br>
<br>
Truth be told, both consumers and investors are not willing to give a social 
venture much of a break. Consumers by and large are not willing to pay a premium 
for an organic, fair trade pound of coffee, even if the profits help at-risk 
children (as Pura Vida Coffee does). <br>
And investors by and large are not willing to take a hit on their investment 
income even if it is a cause they believe in. If you can play on competitive 
terms - say, sell a pound of coffee roughly at the same price as Starbucks - 
then consumers and investors of conscience may very well tilt in your direction 
when it comes the decision point in a financial transaction. Maybe that sounds 
like crumbs off the table, but it points to a significant edge for a social 
enterprise.<br>
<br>
Reduce the friction points - convenience and cost are big ones in the coffee 
business - and you have a good shot at winning new converts. All the same, at 
this point in the paradigm shift, you cannot expect consumers or investors to 
fully blend conscience and commerce. Many folks studiously keep their values and 
generosity private, while operating with a separate playbook in the marketplace.<br>
<br>
(David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States)</font></p>

