		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Piping hot</font></b><font face="Verdana" size="1"><br>
            <b>GIPI pipe mill, Oman’s first ever pipe making venture, is 
            expected to enhance the Sultanate’s aspiration for reducing 
            dependency on import of finished products which are needed to 
            service the strategic oil and gas sector, writes Visvas Paul D Karra</b><br>
            <br>
            <img border="0" src="corp.jpg" align="left" hspace="8" vspace="6" width="300" height="357">Oman’s thriving oil and gas sector relies upon a network of 
            high-pressure pipes which crisscross the length and breadth of the 
            Sultanate to transport the hydrocarbons either to the export 
            terminal, to the refinery, to LNG facilities or to the factories 
            using hydrocarbons as feedstock. Until now all the hydrocarbon 
            producing companies as well as the end consumers had to bear heavy 
            expenditure to import and install a network of pipes to connect to 
            the oil and gas grid. The nearest high pressure pipe manufacturing 
            company, limited to 20” outside diameter, is located in Saudi 
            Arabia. All that is set to change with the entry of Gulf 
            International Pipe Industry (GIPI) established in the Sultanate of 
            Oman in January 2007.<br>
            <br>
            GIPI will not only be the first high-pressure steel pipe mill to be 
            setup in the Sultanate of Oman but it will also be the first pipe 
            mill in the GCC to produce 24” ERW high-pressure pipes. The 
            establishment of the pipe mill is in line with the strategic 
            aspirations of the Sultanate of Oman to reduce dependency on import 
            of finished products needed to service Oman’s strategic oil and gas 
            sector. Plant construction gained momentum in April 2008, following 
            a groundbreaking ceremony attended by over 180 local and 
            international dignitaries. Construction and machine installation is 
            expected to be completed by June 2009 and commercial production for 
            the first phase is scheduled to start in October 2009.<br>
            <br>
            <b>On schedule</b><br>
            Everything is getting ready as per our schedules, says Hamdan Al 
            Shaqsy, managing director and CEO, GIPI while speaking about the 
            progress made to meet the company’s target of starting commercial 
            production in October. The formal opening ceremony will be held in 
            the month of December, 2009.<br>
            <br>
            “One of the unique elements of our company is that we are investing 
            in setting up state-of-the-art production facilities. Because, when 
            we begin our commercial production in October, we will have three 
            months to get our products tested for quality and get the API/ISO 
            and customer (e.g. PDO/Shell) specific certifications which is 
            essential for customers to have these international seal of 
            approvals for our pipes,” informs Shaqsy.<br>
            <br>
                <img border="0" src="corp1.jpg" align="right" hspace="8" vspace="6" width="180" height="352">The manufacturing facilities will include three bays each of over 
            300 metres length, 14 metres high and 25 metres wide, along with a 
            dedicated coating plant. The 5th generation plant machinery is based 
            on American technology, but manufactured in Korea while certain 
            parts are made in US, China, as well as the UK. The overall project 
            cost will stand at $104mn. GIPI uses the high frequency electric 
            resistance welding process to manufacture the carbon steel pipes 
            ranging in nominal diameter from 8 inch to 24 inch and thickness of 
            up to one inch in the first phase of the project, as well as 
            providing other related services like internal and external coating 
            for all pipe ranges and threading facilities for casing pipes.<br>
            <br>
            <b>Stringent tests<br>
            </b>“Oil and gas are highly inflammable products which have to be 
            transported with care through strong pipes as they will pass through 
            towns and villages, through roads, public properties etc. Therefore 
            safety is paramount in these cases. We have to demonstrate the 
            capability by producing three different sizes of the product, which 
            will undergo rigorous lab tests, in order to get the API 
            certification,” Shaqsy says.<br>
            <br>
            Quality control is of utmost importance and GIPI aims to measure up 
            to all the standards. Hence the company is gearing up for ISO and 
            various major customer specific certification in addition to the 
            expected API certification. GIPI is capable of producing over 
            250,000 tonnes per year of products meeting the most stringent 
            international and customer specific standards with the help of the 
            robust technology they are employing. Phase II expansion of the 
            project is being planned within the first five years of operation 
            which will provide GIPI with the capability to produce any pipe 
            ranging from 4” to 48” or 56”. All the raw materials, mostly hot 
            rolled coils of sour grade, will be imported from POSCO in Korea 
            with supply of non-sour grade from identified specialist and 
            certified steel producers within the GCC region, the Indian sub 
            continent, Europe and China. Shaqsy has over 28 years of experience 
            in the oil and gas industry where he served in Petroleum Development 
            Oman for 20 years and held various senior finance roles including a 
            secondment stint of three years working for Brunei Shell Petroleum. 
            Prior to co-founding GIPI, Shaqsy was CFO and secretary to the board 
            of directors at Oman LNG for over six years and was also appointed 
            by the shareholders of Oman LNG to represent Oman LNG’s interest as 
            a shareholder representative in Qalhat LNG.<br>
            <br>
            <b>Conducive market</b><br>
            Shaqsy founded GIPI in partnership with Masoud Al Barwani, who has 
            23 years of experience in the pipe sector. Golden Dunes Investments 
            (GDI), which is the founding company holds 30 per cent share in GIPI 
            while the rest of the shareholders are Oman Investment Corporation 
            (30 per cent), Gulf Investment Corporation (20 per cent); POSCO 
            Steel Services and Sales Company (15 per cent); and Arkan Group (5 
            per cent). The prime target for GIPI products is the oil and gas 
            sectors in Oman and GCC. However, GIPI products will also be 
            suitable for use by emerging customers like Tabreed and is also 
            capable of entering into the water or high grade construction 
            market. Shaqsy disclosed that they have firm letters of intent from 
            major consumers, traders and distributors in the GCC market. 
            Expressions of interest have also come from local and international 
            potential customers especially from the USA market.<br>
            <br>
            “The global economic meltdown has both positive and negative 
            implications for all enterprises and GIPI is not an exception. One 
            element from the positive side is that GIPI will now be ready to 
            solidly participate in projects which have been postponed to 2010 
            and beyond but were earlier due to be floated in 2009 or early 2010. 
            One of the negative implications is that the cost of financing for 
            our working capital will certainly be higher than projected but it 
            is unavoidable,” Shaqsy says.<br>
            <br>
            Presently, GIPI will be employing close to 110 employees with a 
            commitment to meet 35 per cent Omanisation from day one. Being a new 
            company and not wanting to compromise on its production quality, 
            GIPI is in the process of engaging industry experts from India, 
            Philippines, Korea etc. All the Omani employees will get training 
            and eventually the company hopes to have around 50 per cent Omani 
            employees within the next five years. </font> 
