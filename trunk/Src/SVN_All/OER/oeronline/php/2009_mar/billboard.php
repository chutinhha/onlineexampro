                  Centre for Performing Arts, Al Khuwair from March 25-28. A 
                  percentage of the proceeds from this exhibition will be 
                  donated to the charity organisation ‘Daar Al Atta’. “Shades of 
                  Serendib” takes an eclectic vision of Sri Lankan contemporary 
                  art and Semage will be joined by the emerging talents of a 
                  group of Sri Lankan artists staying in Oman. The exhibition 
                  takes a look at the Sri Lankan and Omani art scenes. The works 
                  to be presented can neither be interpreted by occidental 
                  frameworks such as Modernism or Post-Modernism, nor 
                  categorised by the conceptualisation of these artistic 
                  methods. The trend of these paintings rather reflects the 
                  variety of resources which have become the strength of Sri 
                  Lankan art.</font></p>
