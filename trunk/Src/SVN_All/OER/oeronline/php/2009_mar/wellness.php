                <p align="justify" style="margin: 3px 10px"><b>
                <font face="Verdana" size="4">Supple and strong</font></b><font face="Verdana" size="1"><br>
                <b>Jenna Ewing, personal trainer and activities coordinator, Al 
                Bustan Palace Hotel, gives a low down on weight training for 
                executives<br>
                </b><br>
                There are a number of benefits associated with weight training; 
                some of these include better muscle tone and posture, increased 
                bone density, increased production of red blood cells and 
                therefore circulation throughout the body, more effective and 
                efficient muscles allowing an increase in metabolic rate.<br>
                <br>
                <b>Exercise schedule</b><br>
                Depending on the level of fitness, weight training can be 
                carried out anything between 2-4 times a week in order to gain 
                benefits. As fitness levels increase, training specific muscle 
                groups once a week is an option. Rest is just as important as 
                the training, though, as the body needs a chance to repair and 
                recover from the exercise carried out. If there is no rest 
                within a training programme there will be a regression rather 
                than a progression in overall fitness and certain side effects 
                may occur such as insomnia.<br>
                <br>
                Executives that travel a lot should try and move around whilst 
                traveling especially on planes to keep the circulation flowing 
                freely. Once they disembark it is advised to carryout some form 
                of light exercise again to increase the circulation. <br>
                <br>
                Hydration is very important when it comes to exercising. One 
                should drink 500ml of water 30 minutes prior to exercise as the 
                body could lose up to two litres of water in one hour of 
                exercise so it is important to replace this.<br>
                <br>
                <b><font color="#CC3333">Warm up:</font></b> 5-7 reps of a light weight should be carried out as a 
                warm up prior to any weights exercise as this increases the 
                blood flow to the muscle(s) and prepares the body physically and 
                psychologically for the exercise ahead.<br>
                <br>
                <font color="#CC3333"><b>Leg exercises:</b></font> Always start with the larger muscle groups in the 
                body when weight training, this is so that the smaller muscles 
                do not tire too soon. There is a variety of exercises for the 
                legs as with any muscle group including leg press, leg 
                extension, leg curl, calf raise, squats, lunges and many more. 
                The amount of weight and the number of repetitions carried out 
                depends on what the exerciser is trying to achieve. For strength 
                there should be more weight but less repetitions and for 
                endurance there should be less weight and more repetitions, but 
                the muscle should be worked until failure, so it cannot perform 
                any more.<br>
                <br>
                <font color="#CC3333"><b>Back Muscles:</b></font> Initially it is advised to use fixed resistance 
                machines as there is less chance for injury, especially when it 
                comes to the back, as the spine is very precious. Some examples 
                of exercises for the back would be bent over flys, bent over 
                rows, single arm rows. Muscles worked include the Trapezius, the 
                Latisimus Dorsi and the Posterioir Delts. Eight to ten 
                repetitions are ideal for size, while for endurance anything 
                between 15-25 repetitions are adequate, but again the muscle 
                needs to be worked to failure.</font></p>
