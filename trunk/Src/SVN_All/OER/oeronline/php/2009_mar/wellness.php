                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <br>
                <br>
                <font color="#CC3333"><b>Chest and shoulders:</b></font> The bench press and chest flys work the 
                pectoralis major muscles of the chest. Super sets are a good way 
                to target a muscle group more specifically and a lot harder as 
                they use a variety of different exercises to target that one 
                muscle or group of muscles but in a different way e.g. shoulder 
                press and lateral raises. Compound setting is a good way to save 
                time as two opposite muscle groups are targeted like doing bent 
                over flys and chest flys.<br>
                <br>
                <font color="#CC3333"><b>Arms:</b></font> A general workout includes bicep curls and hammer curls. 
                For the triceps one can try tricep extensions or dips.<br>
                <br>
                <font color="#CC3333"><b>Abdominal:</b></font> The abdominal muscles (muscles of the stomach) and 
                the core muscles should be trained last as they need to be 
                strong throughout the session and play a huge role in protecting 
                the spine. A good core stability exercise would include the 
                Plank. Exercises such as Ab Curls and Reverse Curls work the 
                abdominal muscles.<br>
                <br>
                <font color="#CC3333"><b>Cooling down:</b></font> Muscles shorten during exercise so it is essential 
                that some form of stretching is carried out post workout in 
                order to restore, if not, develop one’s flexibility. There are 
                seven main stretching exercises namely – Quadricep stretch 
                (front of thigh), Hamstring stretch (back of thigh), 
                Gastrocnemius stretch (calf), Pectoral stretch (chest), 
                Trapezius stretch (back) and Deltoid stretch (shoulder).<br>
                <br>
                To restore lost energy, one can eat anytime between 15 minutes 
                to two hours after the exercise but if you are trying to lose 
                weight then food should not be eaten until three hours after.
