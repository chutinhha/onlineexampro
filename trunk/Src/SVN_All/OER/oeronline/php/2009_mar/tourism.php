		        <td height="18" valign="top"> <p align="justify"><font face="Verdana" size="1"> 
                    </font><b><font face="Verdana" size="4"><br />
                    </font></b><b><font face="Verdana" size="4">Owning up</font></b><font face="Verdana" size="1"><br>
                    <b>Among the many issues that are crucial to shaping the 
                    future of Oman’s tourism industry is the urgent need to 
                    adopt the principles of responsible tourism to reduce 
                    environmental impact, writes Visvas Paul D Karra<br>
                    </b><br>
                    <img border="0" src="tourism.jpg" align="left" hspace="8" vspace="6" width="350" height="182">Tourism is one of the largest industries in the world today, 
                    employing more than 200 million people and generating over 
                    10 per cent of the world’s total GDP. Travel and tourism 
                    provides a gateway to economic progress particularly in the 
                    areas where there are opportunities for sustainable 
                    development.<br>
                    <br>
                    Experts recognise that the 21st century is bringing major 
                    new challenges and opportunities not only for businesses but 
                    also the world where travel and tourism forms such a crucial 
                    part. It is a well known fact that the tourism sector 
                    creates jobs, boosts economic and social development, 
                    nurtures the environments and preserves cultures and 
                    provides pleasure to hundreds of millions of customers.<br>
                    <br>
                    Even as tourism continues to grow globally, there is a need 
                    to stimulate interactions and discussions between local, 
                    national and international players in the travel and tourism 
                    sector. There is need to do a rigorous assessment of the 
                    impact of travel and tourism on the world we live in and how 
                    the sector is meeting its responsibilities and the need to 
                    do more. It is a well known fact that tourism brings more 
                    and more people onto the ladder of economic growth and help 
                    to break the chain of economic inequalities as tourism’s 
                    trickle-down effect makes it a leading provider of jobs and 
                    prosperity.<br>
                    <br>
                    <b>Minimising environmental impact</b><br>
                    Even as Oman’s tourism continues to grow by leaps and 
                    bounds, managing the natural resources so as to minimise 
                    impact on the environment and “making better places for 
                    people to live in and better places for people to visit” is 
                    fast becoming an important component of the tourism drive. 
                    This is known as “Responsible Tourism” which is largely 
                    defined by the outcomes of two important conferences, in 
                    Kerala (2008) and Capetown (2002), both co-chaired by Dr 
                    Harold Goodwin, who is a world expert in responsible 
                    tourism. The declaration (see box) signed by major tourism 
                    operators and industry leaders requires that operators, 
                    hoteliers, governments, local people and tourists take 
                    responsibility, take action to make tourism more 
                    sustainable.<br>
                    <br>
                    Goodwin is one of the world’s foremost authorities on 
                    responsible tourism, and founder of the International Centre 
                    for Responsible Tourism, based in Leeds in the United 
                    Kingdom. As chair of both the Capetown and Kerala 
                    Responsible Tourism Destination Conferences; and the 
                    Responsible Tourism Awards, Dr Goodwin is widely consulted 
                    as an international leader on sustainable and responsible 
                    tourism development. He has been directly involved in the 
                    creation of both the Capetown and Kerala Declarations – 
                    crucial documents which define global understanding of 
                    responsible tourism.</font></p>
                <p align="center">
                <img border="0" src="tourism1.jpg" width="382" height="402" align="right" hspace="8" vspace="6"></p>
                <p align="justify"><font face="Verdana" size="1">
                    <b>Policy formulation</b><br>
                    Goodwin visited Oman in February and interacted with senior 
                    members of Omran (Oman Tourism Development Company) and 
                    other key opinion makers in Oman. This included a public 
                    talk on responsible tourism, co-hosted by Omran and the 
                    Environment Society of Oman, and a roundtable discussion 
                    with opinion makers and experts on applying responsible 
                    tourism in Oman. The issues raised during these debates have 
                    been noted down and they will hopefully be implemented by 
                    the ministry of tourism to formulate a policy on making Oman 
                    not only a beautiful tourist attraction but also a viable, 
                    sustainable and eco-friendly travel destination for visitors 
                    from all across the globe.<br>
                    <br>
                    Says Goodwin, “Tourism can bring great benefits to 
                    communities, encouraging cultural and social understanding 
                    as well as economic growth. But countries which embrace 
                    tourism must also take responsibility for ensuring that this 
                    tourism is responsible and sustainable.”<br>
                    <br>
                    Omran is currently developing a CSR and Responsible Tourism 
                    Policy which will outline the company’s commitment to the 
                    principles of responsible tourism. The company currently 
                    employs a dedicated CSR Manager, Dana Sarhan, and has 
                    involved itself in a number of social responsibility efforts 
                    since its inception. These include the first paper recycling 
                    scheme in Oman, publication of the Go Green Guide, and 
                    ongoing consultation with local and international experts to 
                    ensure the company’s developments are responsibly developed. 
                    Goodwin believes that Omran has already taken some major 
                    steps forward in demonstrating their commitment to 
                    responsible tourism, and is looking forward to working with 
                    them on their CSR and Responsible Tourism Policy.<br>
                    <br>
                    <b>Priority issue</b><br>
                    Responsible tourism practices can be adopted in different 
                    contexts to deliver innovative solutions based on a 
                    community led approach. There is also immense scope for all 
                    businesses to be more responsible and this should be a 
                    priority issue for all stakeholders.<br>
                    <br>
                    Wael Al Lawati, CEO of Omran, opines that since Oman is 
                    focusing on tourism as a key industry for the coming years, 
                    careful, sustainable growth of this industry will lead to 
                    immense benefits for the nation of Oman and for its 
                    inhabitants. “This growth needs to be planned and managed 
                    responsibly. For this reason we are extremely lucky and 
                    proud to host Dr Harold Goodwin on his visit to Oman, and to 
                    learn from his experience and his extensive academic 
                    background in responsible tourism,” Al Lawati says.<br>
                    <br>
                    On the benefits for the country, Al Lawati says: “As we 
                    finalise our social responsibility programme, I am sure that 
                    Goodwin’s valuable inputs will play an important part in 
                    ensuring this meets the highest international standards. Our 
                    developments make the most of Oman’s natural beauty – it is 
                    one of our most important assets as a company, and 
                    protecting it is therefore crucial to us.” </font></p>
