            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">Qatar 
            Airways to launch Doha-Houston non-stop flights</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>The new Houston link will be the Qatar national carrier’s first 
            foray into southern US, connecting two significant global energy 
            centres</b><br>
            <br>
            Qatar Airways will launch daily non-stop flights between Doha and 
            Houston towards the end of this month even as officials says that 
            the airline will continue to expand albeit the global downturn and a 
            significant reduction in the movement of tourists. Houston will be 
            third US gateway city for Qatar Airways after New York and 
            Washington DC. <br>
            <br>
            Qatar Airways will fly the route using a Boeing 777-200 long range 
            aircraft and will offer Muscat passengers four convenient 
            connections a week. Ali Al Rais, executive vice-president, 
            commercial, Qatar Airways; and Ali Abdul Khaliq Ibrahim, regional 
            manager, Arabian Peninsula and Iran were present during a roadshow 
            organised to announce the new flight.<br>
            <br>
            In the lead up to the much anticipated launch of daily flights to 
            Houston, the Qatar Airways team touched Muscat in February, the 
            final stop on its AGCC-wide roadshow promoting new flights to 
            Houston. During the month-long promotional roadshow, which kicked 
            off on January 11 in Dubai, Qatar Airways visited six cities across 
            the AGCC, meeting key travel trade and industry partners to 
            highlight the new route. <br>
            <br>
            The new Houston link will be the Qatar national carrier’s first 
            foray into the southern United States, connecting two significant 
            global energy centres. The airline currently offers daily non-stop 
            flights between Doha and both New York’s JFK International Airport 
            and Washington Dulles.</font></p>

