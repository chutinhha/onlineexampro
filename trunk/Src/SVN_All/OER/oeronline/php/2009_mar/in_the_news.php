            <br>
                  <b>
                  <img border="0" src="news.jpg" align="left" hspace="8" vspace="6" width="175" height="153">What made Qatar Airways launch daily flights between Doha and 
            Houston?</b><br>
            Our long term strategy is to connect unique destinations. Houston 
            with over 5000 energy related companies is the energy and oil 
            capital of the US. It has a 65,000 strong Arab community. 90 per 
            cent of Qatar’s exports are oil and gas so there are a lot of 
            synergies, between Doha and Houston. The high passenger load factor 
            on our flights to New York and Washington in the US makes us 
            confident about the success of these flights. As these flights are 
            ultra long haul, we had to wait for the right planes, but with the 
            delivery of Boeing 777-200’s we are now able to fly such long 
            trans-continental routes.<br>
            <br>
                  <b>What are Qatar Airway’s plans in the coming years?<br>
                  </b>The government of Qatar is spending $500mn on the expansion of Doha 
            Airport. The new Doha International airport is expected to open by 
            2011. Qatar Airways will fly to 120 plus destinations in the next 
            five years. We expect 110 new aircrafts to join our service and we 
            have placed orders worth $40bn to acquire these new planes.<br>
            <br>
                  <b>Do you see low cost airlines posing a challenge to full fleet 
            carriers like Qatar Airways in the light of the worldwide financial 
            crisis?</b><br>
            The two models are different, ours is a product that offers you a 
            five star treatment. It is a customer’s decision as to what kind of 
            an experience he wants while flying. The low cost model is too new 
            in the region and we still need to see whether it will work or not.
            <br>
            <br>
                  <b>What is Qatar Airway’s differentiation?</b><br>
            Our focus on customer service is our biggest differentiator. For 
            example, we built a new terminal for our first and business class 
            passengers so that they could enjoy the finest cuisine and other 
            facilities. We have introduced a service wherein you can get your 
            boarding pass on the mobile phone.</font></td>
