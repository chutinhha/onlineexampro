                <font size="4" face="Verdana, Arial, Helvetica, sans-serif">Oman to launch brand ambassador programme</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>Brand ambassadors will tour the length and breath of the country to 
            promote the brand mark in order to build an informed national 
            identity and make every Omani individual a brand ambassador. An OER 
            report<br>
            </b>
            <br>
            The Oman Brand Ambassador Programme (OBA) is being launched by the 
            Oman Brand Management Unit (OBMU) to help build a compelling and 
            authentic brand story for Oman. This brand story will be the 
            foundation on which a powerful and convincing nation branding 
            awareness campaign will be built upon.<br>
            <br>
            To promote Oman’s best stories, 20 sector Brand Ambassadors will be 
            recruited. Together, they will present the dynamic and attractive 
            face of Oman. By sharing Oman’s business, tourism, education, 
            technology and cultural experiences the Brand Ambassadors will 
            showcase Oman’s distinctive characteristics. These 20 Brand 
            Ambassadors will tour the Sultanate and make presentations at 
            schools, colleges, businesses and government organisations – 
            promoting the quality, excellence and distinctiveness of the 
            Sultanate. <br>
            <br>
            The perceptions people have of Oman are critical to our ability to 
            be recognised as a country in which to live, work, invest and visit. 
            In this regard, it is imperative that we explore and understand 
            perceptions of the Sultanate and take immediate action to address 
            our image and profile nationally and internationally. “Launching the 
            OBA initiative will be instrumental in helping Oman correct 
            misperceptions and present an accurate and informed national image – 
            one that we can be justifiably proud of,” says His Highness Sayyid 
            Faisal Al Said, CEO, Oman Brand Management Unit (OBMU), while 
            jointly announcing the launch of the programme along with HE Mohsin 
            Khamis Al Balushi, adviser, ministry of commerce and industry.<br>
            <br>
            “The brand ambassadors will be significant resources in helping 
            build Oman’s credentials nationally and internationally. These are 
            experienced and forward-thinking people that truly embody the spirit 
            of Brand Oman,” says Lamya Al Habsi, OBA Programme Manager. <br>
            <br>
            <b>National brandmark workshop</b><br>
            The OBMU also organised a free brandmark guidelines workshop for 
            marketing, communication, advertising, media, PR and print 
            professionals last month. According to OBMU’s Sara Al Kalbani and 
            organiser of the guidelines workshop: “The newly launched national 
            brandmark will play a pivotal role in enhancing Oman’s international 
            identity.” In simple terms, this is an opportunity for people to 
            understand how the brandmark and its associated typefaces, colour 
            palette and photographic images are to be used. “If we’re to be 
            successful in projecting a unified and coherent national image then 
            consistency in the use of the brandmark is absolutely imperative,” 
            says Al Kalbani.<br>
            <br>
            With the impact of globalisation and the rise of the attraction 
            economy, it’s increasingly important that Oman gets it international 
            image right. “Let’s be clear, consumers are attracted to clear and 
            consistent messages about things they value, from competent 
            governance, to friendly and hospitable people, to financial 
            transparency and investment opportunities. It’s in these areas that 
            OBMU will be working with public and private sector partners to 
            present unified, coherent and engaging messages,” Al Kalbani 
            commented. <br>
            <br>
            OBMU will be organising similar workshops to introduce the brandmark 
            to Oman’s business community, government organisations and the 
            public in general. </font></p>

