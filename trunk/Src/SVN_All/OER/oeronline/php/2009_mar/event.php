				<p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4">
				<br>
				</font></b><b><font face="Verdana" size="4">Blueprint for 
                Success</font></b><font face="Verdana" size="1"><br>
                <b>Renowned speaker and motivator Shiv Khera has a recipe for 
                managing the global crisis which he will reveal at an excluvise 
                workshop. An OER report<br>
                </b><br>
                <img border="0" src="event2.jpg" align="right" hspace="8" vspace="6" width="210" height="450">Winners don’t do different things, they do things differently, 
                says Shiv Khera, the internationally acclaimed author, educator, 
                business consultant, a successful entrepreneur, a much 
                sought-after speaker, and a great motivator, who will be in 
                Muscat on March 30 for a day-long workshop. The title of the 
                workshop is, ‘Blueprint for success - turn setbacks into 
                comebacks’. The workshop would be conducted at the Majan 
                Ballroom of Al Bustan Palace Hotel and is being organised by NPA 
                Events.<br>
                <br>
                Injecting new vitality in your organisational culture based on 
                ethics and integrity is paramount because of the global 
                financial crisis which has seen the death of many companies 
                resulting in retrenchment of staff bringing down the morale of 
                existing workers. The global doom phenomenon has not left anyone 
                untouched and it is in this light that this workshop becomes the 
                need of the hour.<br>
                <br>
                Experience shows that throughout history, success has resulted 
                from overcoming problems. Bigger the problems, the bigger the 
                opportunities. The more problems a person solves the greater the 
                reward he receives either in money, or in goodwill. Today 
                recession lends itself to one of the greatest opportunities to 
                those who are solution- focused and those who can answer the 
                question how it can be done, rather why it cannot be done.<br>
                <br>
                Qualified Learning Systems, Shiv Khera’s flagship company has 
                custom developed a core programme known as the ‘Blueprint For 
                Success – Turn setbacks into comebacks’. This workshop is aimed 
                at motivating the participants to identify their true potential 
                and achieve success both in their personal and professional 
                spheres of life.<br>
                <br>
                <b>Positive focus</b><br>
                The focus areas of this seminar would be on how to manage and 
                triumph over these turbulent times, given the present economic 
                slowdown and the overall negative sentiments that are prevalent. 
                At this workshop, Shiv Khera would put to practical use his 
                wide-ranging experiences in human psychology and in boosting 
                people’s sagging morale and confidence. By analysing the issues 
                and by showing the participants the way to come up with workable 
                solutions, which can be drawn from within oneself, this one-day 
                workshop would provide that much-needed relief to the 
                participants. The thrust area of Shiv Khera’s message would be 
                on focusing on the positive aspects more, rather than dwelling 
                on the negatives.<br>
                <br>
                The thrust of the workshop would be on how to ‘Optimise Your 
                Performance’, ‘Gain A Winner’s Edge’, and ‘Achieve Excellence By 
                Managing Differently’. The main topics covered during this 
                seminar would revolve around the theme of the three ‘A’s as 
                prescribed by Shiv Khera: Attitude; Ambition; Action.<br>
                <br>
                Also other interesting concepts on ‘performance’ and 
                ‘leadership’, the differences between ‘authority’ and 
                ‘influence’ and how to bring ‘personal values’ and ‘ethics’ at 
                work would be discussed and debated. NPA Events is pleased to 
                announce this workshop which follows the success of the 
                management events ‘Up Your Service’ with Ron Kaufman; 
                ‘Re-imagine’ with Tom Peters with Competence HR; and ‘Filling 
                the Glass for Leadership’ by Barry Maher, which were earlier 
                organised by them. The Shiv Khera seminar will be attended by 
                high profile corporate honchos, senior decision makers, top and 
                middle level management personnel from Oman’s vibrant corporate 
                sector.<br>
                <br>
                Through this seminar, participants could get lasting results 
                through positive behavioural changes. By learning how to develop 
                a culture that creates trust, immense benefits will be gained 
                not only by the participant, but also his/ her colleagues and 
                the organisation itself. Sections that could be of great use to 
                the organisation, through the participants, are about 
                ‘empowering people and making them accountable’.<br>
                <br>
                At a more personal level, themes of ‘inspiring, influencing and 
                getting results’ and the knack of ‘building powerful teams that 
                go the extra mile’ would make this seminar not just an 
                unforgettable one, but is sure to make the participants to 
                transform for the better.<br>
                <br>
                <b>Global message</b><br>
                Shiv Khera has taken his dynamic personal message to opposite 
                sides of the globe, from the US to Singapore. His 30 years of 
                research, understanding and experience has helped people on the 
                path of personal growth and fulfillment. Many thousands have 
                been benefited from his dynamic workshops internationally and 
                over two million people have heard him as a keynote speaker.<br>
                <br>
                Shiv Khera holds a veritable Who’s Who list of corporate clients 
                including a number of diplomats who swear by his teaching 
                methods. Client list includes IBM, HSBC Bank, GM, Gillette, 
                Nestle, Johnson &amp; Johnson Ltd, Lufthansa German Airlines, 
                Carrier, Deutsche Bank, Ericsson, Tetrapak, GSK, Bahamas Quality 
                Council, Dubai Quality Award amongst others.<br>
                <br>
                He has been recognised as a “Louis Marchesi Fellow” by Round 
                Table Foundation, an award given to among others like Mother 
                Teresa. Lions Club International has honoured Khera with 
                “Lifetime Achievement Award” for the cause of ‘Humanitarian 
                Service to the Society’. Rotary Club has honoured him with the 
                “Centennial Vocational Award for Excellence”. Shiv Khera is also 
                the author of the international best seller ‘You Can Win’ which 
                has sold over one-and-half million copies in 14 languages 
                worldwide.<br>
                <br>
                Says Ashok Suvarna, Executive Director, NPA Events, “It is our 
                constant endeavour to bring to Oman only the finest and the best 
                quality speakers. Especially during these financially unsettling 
                times, we felt that bringing someone of Khera’s calibre would be 
                the most ideal thing, given the fact that Khera is among many 
                others, a motivator par excellence, and a very talented speaker 
                who can provide ways and means of coping with the economic 
                slowdown. The one-day workshop will stand out for its sheer 
                entertainment value and worth”.</font></p>
