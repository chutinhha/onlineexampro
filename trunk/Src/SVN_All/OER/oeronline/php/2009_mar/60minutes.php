            <td width="492" bgcolor="#FFFFFF" valign="top"> <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <br>
                </font><b><font face="Verdana" size="4">On a high note<br>
            </font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>Turkish Oman business relations have hit an all time high with 
            bilateral trade more than doubling in 2008 from the previous year, 
            writes Visvas Paul D Karra after a free-wheeling chat with HE Engin 
            Turker, the Ambassador of the Republic of Turkey<br>
            </b><br>
            <img border="0" src="60minutes.jpg" align="left" hspace="8" vspace="6" width="300" height="261">The Turko-Oman relations have never been business like. They have 
            always been cordial and brotherly and we are both content to carry 
            this special relationship to farther horizons,” says His Excellency 
            Engin Turker, Ambassador of Turkey in his opening remarks to Oman 
            Economic Review. And the Ambassador is absolutely right because the 
            Turkish presence in Oman has been in the limelight on many occasions 
            not only because of the business deals but also because of the 
            various cultural activities organised by the embassy.<br>
            <br>
            On Valentine’s Day, renowned Turkish jazz composer and pianist Kerem 
            Gorsev performed in Muscat for a selected group of audience who were 
            wowed by the music. HE Turker avers that the concert is a gift to 
            young and old lovers and believes that pleasant occasions as such 
            promote the arts and culture which in turn further helps to 
            strengthen the good Turko-Oman relationship. “Music is 
            international. It touches the heart and anyone with an ear for it 
            can enjoy music. It builds bonds,” he says.<br>
            <br>
            Forthcoming art exhibitions, Turkish Film Days and a fantastic joint 
            musical project with Royal Symphony Orchestra are in the pipeline. 
            On the business front, more than 12 Turkish companies are active in 
            the Sultanate of Oman with contracts worth over $2.5b in their 
            kitty. The bilateral trade between Turkey and Oman has grown by more 
            than 137 per cent in 2008. Further, the 3rd round of the 
            Oman-Turkish Business Council meeting was held in Muscat in February 
            where more than 30 Turkish companies visited Oman. All of them went 
            back with satisfactory business meetings.<br>
            <br>
            According to HE Turker, the Omani-Turkey economic relations are 
            galloping at the moment and there is a lot which is happening. The 
            trade delegation that visited Oman, between February 8-12, 2009, 
            held a number of meetings with their Omani counterparts. They held 
            meetings with HE Khalil Al Khonji, chairman of Oman Chamber of 
            Commerce and Industry (OCCI); the undersecretary and the relevant 
            director general of the Ministry of Oil and Gas; with officials of 
            the Tender Board; and the Omani Centre for Investment Promotion and 
            Export Development.<br>
            <br>
            <b>Immense interest<br>
            </b>The extent of the friendly relations can also be gauged from the 
            fact that members of Turkish business delegation were received by HE 
            Dr Rajiha bint Abdulamir bin Ali, Minister of Tourism and also by 
            undersecretaries and high officials of the Ministry of Transport and 
            Communications; State General Reserve Fund, Public Establishment for 
            Industrial Estates; and Sohar Port. “The interest is immense in both 
            countries in various sectors like tourism, real estate, trade 
            industry,” says HE Turker.<br>
            <br>
            <img border="0" src="60minutes1.jpg" align="right" hspace="8" vspace="6" width="300" height="308">He added that two important agreements vital for mutually 
            beneficiary business relations, the “Avoidance of Double Taxation” 
            and the “Protection and Promotion of Reciprocal Investments”, have 
            already been concluded much earlier between Turkey and Oman, paving 
            the way to healthy business relations. He also cited that 
            “Turkey-Oman Joint Economic Committee” is also another dynamic forum 
            which has been working on ways and means to enrich various 
            dimensions of bilateral economic relations. <br>
            <br>
            He happily mentioned that both Omani and Turkish sides have 
            established mutually beneficial partnerships investing in fruitful 
            business areas, beyond the borders of their respective countries.<br>
            <br>
            The bilateral trade between Oman and Turkey has risen from $103mn in 
            2007 to $227mn in 2008, a jump of 137 per cent. More than 90 per 
            cent of this trade volume is exports from Turkey to Oman, wherein 
            nearly 300 items are exchanged between the two countries. The bulk 
            of this trade is in building materials like iron and steel and 
            industrial products as well as consumption and food products.<br>
            <br>
            <b>New vistas</b><br>
            The construction sector has been the forte of the Turkish Companies 
            active in the Sultanate. Turkish Construction Companies’ interest in 
            the Sultanate is ever growing. He added that two more important and 
            experienced Turkish companies are in the process to establish 
            themselves in the Sultanate. Most recently TAV Construction has been 
            short-listed among six competitors for the upcoming majestic project 
            of Muscat International Airport. <br>
            <br>
            Now many of the entrepreneurs are looking at new dimensions. Many 
            Turkish oil companies with vast experience in Central Asia, Iraq, 
            North Africa are now toying with the idea of entering into Oman’s 
            energy sector, mainly exploration. “The government of Oman has 
            announced five new concessions namely Blocks 36, 38, 39, 42 and 50. 
            Our energy companies are eyeing these concessions” disclosed HE 
            Turker. <br>
            <br>
            The defense industry is another area with business potential for 
            Turkey which is keen to have cooperation with Oman’s defence 
            ministry. Turkey has a hi-tech defence industry which manufactures 
            armoured vehicles and all kinds of army equipment and elements. This 
            could add a new dimension to bilateral cooperation.<br>
&nbsp;<br>
            One of the best things about the Turkish-Oman economic relations is 
            the growing interest of both countries to participate in 
            international trade fairs. In 2006, the Istanbul Chamber of Commerce 
            and Industry held an exhibition of Turkish items in Muscat and the 
            following year, the first Omani products exhibition was organised in 
            Istanbul. This year, the Turkey Tourism Board has participated in a 
            major way at the Oman Travel Mart (OTM) 2009, the international 
            tourism event of the year. This annual travel mart attracts nearly 
            10,000 visitors in a span of three days.<br>
            <br>
            Turkish representation will also be in abundance at the Buildex and 
            Interiors 2009, the mega annual event which brings together everyone 
            and anyone who deals with construction and the real estate sector. 
            The OCCI is also expected to send a big delegation to participate in 
            the renowned Izmir International Fair, which will be held from 
            mid-August to September.<br>
            <br>
            <b>Cultural diplomacy</b><br>
            The Turkish Ambassador is as adept at facilitating trade and 
            business deals as he is at organising art and cultural shows. During 
            the last four years that he has been in Oman, HE Turker has been 
            responsible for organising many cultural events which showcased the 
            rich cultural diversity of modern Turkey, which sits at the 
            crossroads of civilisations, bearing the richness of both east and 
            west. This unique characteristic is visible in everything related to 
            Turkey – in art exhibitions, film festivals, musical fests, 
            conferences or fashion shows. <br>
            <br>
            HE Turker opines that as an ambassador, it is his utmost duty to 
            ensure and enhance the all round relations between his country and 
            Oman. Ending on a more philosophical note, he says: “The 
            relationship between Oman and Turkey is deep-rooted and it is ever 
            developing with a mutual sense of respect and warm sentiments. I am 
            just doing my best to contribute to this beautiful atmosphere 
            between our two nations. Perfection is infinitive and whatever is 
            done will be improved upon in the future. My love for my country and 
            my affection to the Sultanate and my deep admiration and respect to 
            His Majesty Sultan Qaboos bin Said always inspired me in my loyal 
            service that I have tried to render in the best possible manner.” </font><br>
