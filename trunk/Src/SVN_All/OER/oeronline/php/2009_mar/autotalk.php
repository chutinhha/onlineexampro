				<p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4">
				<br>
				</font></b><b><font face="Verdana" size="4">Little bruiser<br>
                </font></b><font face="Verdana" size="1"><b>Mercedes recently 
                launched their latest, and smallest, SUV in their lineup. 
                Malcolm Xavier Crasta dives it in Salalah</b><br>
                <br>
                <img border="0" src="auto.jpg" align="left" hspace="8" vspace="6" width="400" height="259">At the end of November last year we were invited to Salalah for 
                the launch of the all new Mercedes GLK. It is the latest soft 
                off-roader to enter the Mercedes lineup and is aimed squarely at 
                the likes of the BMW X3, Lexus RX330 and the Audi Q5. To be 
                honest we weren’t expecting anything special from it and thought 
                it to be a smaller version of the other SUV’s in the lineup, 
                sort of like a baby ML. <br>
                <br>
                We got our first look at the car when we were picked up at the 
                airport at Salalah. It fit the bill perfectly, but rather than a 
                baby ML it was more like a baby GL. While it is not a great 
                looking car, it is definitely not ugly. For lack of a better 
                description it could be called rather handsome but somewhat 
                anonymous. And the same follows through on the interior design 
                as well. <br>
                <br>
                While sitting in the back it was quite clear that tall people 
                might have problems with leg and headroom. Despite this, it was 
                quite confusing why the boot wasn’t bigger. At 450l capacity, it 
                is average at best, but should be more than sufficient for your 
                regular town runs just don’t expect to fit your cross-country 
                baggage in there with ease. But these are minor inconstancies 
                when you consider the quality of the fitments inside and the 
                comfort of the seats that you have sunk into. Just as long as 
                you are not too tall, it is quite easy to get comfortable in the 
                back seats for a long trip. Our first hands on experience with 
                the car came the next day when we took the cars on a tour around 
                Salalah. On the road it is just what you would expect, competent 
                and stable, but weighing in at a portly 1,830kg it can be quite 
                sluggish with the smaller of the two engines. The GLK280 is 
                equipped with a 3.0l V6 rated at 231hp and 300Nm of torque. But 
                apart from this it is practically identical to the GLK350, which 
                is equipped with the 3.5l V6 that is rated at 272hp and 350Nm of 
                torque. What is most confusing is that the price difference 
                between the two is just USD2,450 (RO945) which makes the lower 
                model all but pointless.<br>
                <br>
                <b>Off-road capability<br>
                </b>It is off road that the vehicle truly outperformed our 
                expectations. We took it up steep hills, through wadis and over 
                boulders and it ate all up without breaking a sweat. We would go 
                so far as to say that it will put many, larger and more capable, 
                off-roaders to shame and was basically only limited by its 
                ground clearance and the large rim sizes. As an option Mercedes 
                also offers two off-road packages. One is a styling package, 
                which as the name implies, doesn’t give you much else other some 
                chrome and simulated underguards. The other is the more 
                substantial ‘off-road engineering package’ which basically 
                transforms the vehicle into an even more aggressive terrain 
                conqueror. Included in the package is an off-road button which 
                changes the shift points of the 7G-Tronic transmission and 
                softens the acceleration of the vehicle. It also activates the 
                off-road ESP function, which includes the off-road ABS and 4ETS 
                system. In this mode there are fewer interventions involving the 
                engine management system and the system also tolerates higher 
                degrees of wheel-slip. But, one of the most impressive features 
                of the package is the ‘Downhill Speed Regulation’ (DSR) System. 
                This system automatically maintains pre-programmed speeds 
                between 4 to 18kmph on steep downhill inclines and can be 
                controlled using the cruise control lever. Unfortunately, 
                Mercedes made one mistake in the GLK that severely limits its 
                off-road capability and that is not including a spare wheel, a 
                temporary tire will just not cut it. Given, most of the buyers 
                of this vehicle will not really take this vehicle off-road but 
                those that want to experience its, truly impressive, off-road 
                capability will be limited by this important omission.<br>
                <br>
                Apart from its off-road features and capability everything else 
                is pretty much the standard fare in terms of features and 
                quality that you expect from Mercedes. And overall it is quite a 
                good vehicle on and off the road. While it may not be cheap by 
                any standards, it still makes for a good alternative to anyone 
                who wants more practicality than the C-class, on which the 
                platform of the GLK is based. Just be sure to go for the 350 
                rather than the 280 since the price difference isn’t all that 
                much and skip on the off-road package if you don’t intend to do 
                some serious off-roading since the basic car itself is quite 
                capable handling the basic family wadi trip. </font></p>
