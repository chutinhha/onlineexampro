		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Glimmer of hope<br>
                </font></b><font face="Verdana" size="1"><b>The government’s 
                stabilisation fund and strong results by large corporate helped 
                the MSM index make marginal gains</b></font><br>
                &nbsp;<br>
                <font face="Verdana" size="1">The MSM benchmark index recovered 
                after testing four year lows of 4,180 points in the month of 
                January 2009, to bounce back and close above 4,800 points, 
                though down 3.16 per cent for the period from 16th January 2009 
                to 15th February 2009. Industrial and service sector stocks led 
                the fall with banking sector stocks offsetting the weakness. <br>
                <b><br>
                Shot in the arm </b><br>
                Government’s investment stabilisation fund aimed to support the 
                local market while boosting investor confidence. The policy 
                decision paid rich dividends as the benchmark index surged more 
                than 16 per cent from the lows of 4,180 points. Average daily 
                turnover on the MSM increased from RO2.9mn to RO3.5mn for the 
                period from 16th January 2009 to 15th February 2009.</font><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">&nbsp;</font><p align="center" style="margin: 3px 10px">
                <img border="0" src="market.jpg" width="373" height="358"><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">Market sentiment turned positive after heavyweights like Omantel, 
                Renaissance Services and Bank Muscat reported a rise in profit 
                in 2008. Bank Muscat reported 11.2 per cent increase in net 
                profit to RO93.7mn. Bank Muscat’s operating profit increased by 
                48 per cent year-on-year to RO 156.2mn in FY08. Renaissance 
                services reported a 51 per cent rise in profits to RO26.19 and 
                the management announced its plans to pursue expansion plans. 
                Omantel reported a 4 per cent increase in net profit to 
                RO116.7mn while its operating profits increased by almost 26 per 
                cent year-on-year to RO157.65mn in FY08. Local and Arab 
                investors were net buyers in January 2009 while foreigners and 
                GCC nationals were net sellers.<br>
                <br>
                <b>Corporate news</b><br>
                In key corporate events, Al Hassan Engineering Company announced 
                that its subsidiary Al Hassan Engineering Co Dubai (49 per cent 
                owned) has won a contract in UAE for approximately RO8.9mn from 
                Dubai Electricity &amp; Water Authority for civil works in Dubai 
                Land II 400/132 KV substation. <br>
                <br>
                In other major corporate event, Port of Salalah joined hands 
                with container shipping leader APL to operate a new two-berth 
                container terminal which will be operational by 2011. The joint 
                venture aims to cover a 28-hectare deep sea facility that will 
                have a total quay length of 700 metres and an annual capacity of 
                1.6 mn TEU (twenty-foot equivalent unit).</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
                <img border="0" src="market1.jpg" width="475" height="340"><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1"><br>
                <b>GCC markets <br>
                </b>The month ended February 15, 2009, registered lackluster 
                performance for all the GCC bourses with the benchmark MSCI GCC 
                index plummeting 5.22 per cent to 314.27 points. The DFM was the 
                ace loser and the general index plummeted 10.05 per cent during 
                the month. Sell-off was seen across counters with the investment 
                and real estate indices registering a dip of 18.08 per cent and 
                13.39 per cent respectively.<br>
                <br>
                The Bahraini general index occupied the second position after 
                DFM and the general index declined 7.92 per cent. All the major 
                indices were in red with the banking counters taking the major 
                hit and losing 17.89 per cent. <br>
                <br>
                The Kuwaiti and Abu Dhabi stock exchanges too declined by 6.68 
                per cent and 6.22 per cent respectively. While the investment 
                index was the worst performer on the Kuwaiti exchange losing 
                10.75 per cent, real estate counters where prime losers on the 
                Abu Dhabi exchange and ended down 25.92 per cent. <br>
                <br>
                The DSM 20 Index, which is the highest YTD loser among the GCC 
                dipped 5.59 per cent during the period. The insurance counters 
                were among the top losers and the index declined by 9.03 per 
                cent. The Tadawul All Share Index, the benchmark index of Saudi 
                Arabia ended flat down 0.79 per cent. The broader indices ended 
                mixed with insurance and industrial investments indices ending 
                up 17.35 per cent and 8.73 per cent respectively. <br>
