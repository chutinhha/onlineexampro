                    </font><b><font face="Verdana" size="4"><br />
                    Affordable segment is hot</font></b><font face="Verdana" size="1"><br>
                    <b>Ali Hassan Moosa, General Manager, Alargan Towell 
                    Investment Co. talks about the recent trends in real estate 
                    market in Oman besides updating on the company’s various 
                    projects in a chat with Akshay Bhatnagar<br>
                    </b><br>
                    <b>
                    <img border="0" src="realestate.jpg" align="left" hspace="8" vspace="6" width="400" height="294">What are your views on the impact of economic slowdown on 
                    the real estate sector in Oman?<br>
                    </b>We all know that credit crunch has affected the entire 
                    world. But Oman is a different market and the economy is 
                    based on strong fundamentals. Though Oman is feeling the 
                    heat due to the global phenomenon in the short term but in 
                    the long term the Sultanate will get over it. If you look at 
                    the State Budget for 2009, the government has not cut down 
                    on the spending which ensures that there will be adequate 
                    flow of money in the economy as the local market thrives on 
                    government spending. The good thing is that due to easing of 
                    inflationary pressure, the cost of consumer and industrial 
                    products have dropped to a considerable extent. The costs of 
                    construction and building materials have gone down for the 
                    developers which could be passed on to the consumers. The 
                    banks in Oman are strong enough to withstand the pressure 
                    and I’m hopeful that issues such as liquidity and cost of 
                    borrowing will be resolved in the coming months.<br>
                    <br>
                    <b>Tell us about Alargan Towell Investment Company.</b><br>
                    Alargan Towell Investment Co is a six-year old company. It 
                    is a joint venture between WJ Towell &amp; Co. and Kuwait based 
                    Alargan International Real Estate. It started with an 
                    objective to develop middle income real estate housing 
                    solutions. But it has widened its scope to develop other 
                    kinds of top-end, upper middle and hospitality oriented 
                    properties also.<br>
                    <br>
                    <b>What makes you think that the middle income segment is an 
                    exciting prospect for your business?<br>
                    </b>We believe that there is a high demand for affordable 
                    housing among the middle income segment. Many Omanis who 
                    work in Muscat don’t own a property in the city but they are 
                    interested in buying a quality residential unit. They need 
                    to take the mortgage route to finance the purchase and are 
                    comfortable in paying an equated monthly installment that is 
                    equal to their monthly rentals. The demand in this segment 
                    will continue to grow as Oman has a sizable young population 
                    and people are moving away from joint families to the system 
                    of nuclear families. If you consider the GDP per capita of 
                    Oman (as an economic indicator), it is relatively low 
                    compared to other countries in the GCC.<br>
                    <br>
                    <b>How do you define middle income and affordable housing in 
                    value terms?</b><br>
                    There is no fixed formula to describe the two. Generally, 
                    middle income denotes a monthly family income of RO800-1500. 
                    The affordable housing for the middle income is in the range 
                    of RO50,000-120,000 depending upon the dwelling.<br>
                    <br>
                    <b>Do you think there is a need to come up with more reforms 
                    in the legal framework to promote property ownership among 
                    the expatriates also?</b><br>
                    The regulatory mechanism and legal framework in Oman as far 
                    as foreign ownership of property is concerned is very well 
                    defined and developed compared to other countries in the 
                    region. Foreigners are allowed to purchase properties in 
                    integrated tourism complexes (ITCs). However, we can look at 
                    creating housing units that are more affordable for 
                    foreigners in the ITCs. But it is not easy for the developer 
                    as he has to build the township on a very large scale to get 
                    ITC status and thus offer a freehold property. This calls 
                    for a huge investment and to recover his capital investment, 
                    the developer has to maintain a very high quality and keep 
                    the pricing at a high level too.<br>
                    <br>
                    <b>Is there a way out for the expatriates in a non-ITC 
                    project?</b><br>
                    It is possible to offer affordable housing to an expatriate 
                    in a non-ITC project through the lease agreement route. Laws 
                    and regulations in this regard need to be formulated.<br>
                    <br>
                    <b>Are you offering such products?</b><br>
                    Yes. Very soon Alargan Towell Investment will become the 
                    first developer in Oman that will offer an expatriate 
                    legally registered lease option. We plan to come up with 
                    Barka Resort, a mixed use development, which will offer such 
                    option. The project will have marina with 100 berths. Whole 
                    development is around 620,000 sq m mixed use with different 
                    components like high end luxury beachfront and waterfront 
                    with private berth for yacht. It will have townhouses, small 
                    villas and apartments for middle income. Dimnyiat Island is 
                    only about 20 minutes away, by boat, from here. We expect to 
                    make an official announcement on the lease option sometime 
                    in the first or second quarter of the year depending on 
                    obtaining the necessary permission from the relevant 
                    authorities.<br>
                    <br>
                    <b>What makes your Al Khiran hotel a unique one?</b><br>
                    We have a pristine site. It is an environmentally protected 
                    area. We have one of the best operator (The Oberoi’s) and 
                    best design (by Bernard Khoury/DW5). From the road you can’t 
                    see any building as you approach the site. You will first 
                    get the elevator and it will take you down to the building. 
                    People approaching from the sea will see a nice development. 
                    The project is now registered with the US Green Building 
                    Council that promotes how green buildings are designed, 
                    built and operated as per the highest international 
                    standards. We are going to be Oman’s first green hotel. </font></p>
