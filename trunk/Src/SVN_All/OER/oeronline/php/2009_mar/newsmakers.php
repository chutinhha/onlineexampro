            <b><font face="Verdana" size="4">Muscat Festival: Good show<br>
            </font>
            </b><font face="Verdana" size="1">
            <img border="0" src="news2.jpg" align="right" hspace="8" vspace="6" width="170" height="221">The Muscat Festival, which concluded in February, has attracted 
            more than a quarter million of visitors despite a scaled down 
            version of the annual extravaganza. This time, even though the 
            festival was limited to only three venues spread across the capital 
            area of Muscat, the festival goers remained undeterred.<br>
            <br>
            Visitors, both local residents as well as international tourists, 
            thronged the two main venues namely the Qurum National Park and the 
            Naseem Gardens located 20 kms away from the capital of Muscat. <br>
            <br>
            The outdoor events as usual were arranged at the Seeb Beach, located 
            adjacent to The Wave Muscat. Visitors came from as far away as 
            Salalah and almost all other interior places of the Sultanate to 
            experience the state-sponsored entertainment cum shopping fiesta. 
            Kudos go to Sultan bin Hamdoon Al Harthy, the newly appointed 
            chairman of Muscat Municipality for ensuring a flawless and incident 
            free event which has drawn international acclaim for being a 
            festival in the true sense.<br>
            <br>
            The festival comprised of many events like cultural shows, 
            commercial exhibitions, international pavilions and ofcourse 
            international cultural shows. There was brisk business at the 
            commercial exhibition centres with visitors scouting for exotic 
            items for home décor as well as bargain prices for household goods. 
            Both children and adults enjoyed the cultural shows, which included 
            folk and contemporary ensembles of various countries. A number of 
            stunt shows and laser shows also wowed the audiences at all the 
            places.<br>
            <br>
            Arab singers and musical bands drew huge crowds of people to the 
            Seeb beach where these live shows were organised on Thursdays and 
            Fridays. </font></p>
