            Bernard Kouchner, Foreign and European Affairs Minister; Herve 
            Morin, Minister of Defence; Anne Marie Idrac, Minister of State for 
            External Trade; Malika Berak, French ambassador to the Sultanate; 
            Herve de Charette, Deputy in the National Assembly of Maine-et-Loire 
            Province; Admiral Eduard G, Special Chief of Staff at the Republican 
            Presidency; Jean David Levitte, Diplomatic Adviser of the President; 
            Frank L’ouvrier, Adviser of the Republican Presidency; and Cedric 
            Goubet, Head of the President of the Republic.<br>
            <br>
            His Majesty The Sultan and Sarkozy held an official session of talks 
            at Al Alam Palace. During the session, the two discussed ways of 
            strengthening bilateral relations between the two countries and all 
            aspects of existing bilateral cooperation in various domains in the 
            joint interest of the Omani and French friendly peoples.<br>
            <br>
