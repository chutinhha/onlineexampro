            at Al Alam Palace guesthouse by His Majesty The Sultan.<br>
            <br>
            Members of the Royal Family, the chairmen of the State Council and 
            Majlis A’shura, ministers, commanders of the Sultan’s Armed Forces, 
            the Royal Oman Police and members of the mission of honour were 
