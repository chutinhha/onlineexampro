                <td height="18"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4">Suite above the 
                clouds</font></b><font face="Verdana" size="1"><br>
                <b>Swiss International Airlines unveiled a new First Class cabin 
                at a world press conference at Zurich, Switzerland, recently. 
                SWISS CEO, Christoph Franz spoke to Mayank Singh on the 
                sidelines of the event<br>
                </b><br>
                <img border="0" src="aviation.jpg" align="left" hspace="8" vspace="6" width="350" height="241">SWISS will be gradually introducing a new First Class cabin from 
                this spring onwards in parallel with its fleet renewal 
                programme. The centrepiece of the new cabin product is the new 
                SWISS First Seat, which offers exclusive design, top in-flight 
                comfort and extensive privacy and can be individually adjusted 
                to travellers’ specific wishes and needs. With its numerous 
                settings and comprehensive adjustability, the new seat offers 
                in-flight comfort that can be specifically tailored to each 
                guest’s individual preferences. The seat can be reclined at the 
                touch of a button into a totally lie-flat bed that is more than 
                two metres long. It is also equipped with a down-filled duvet, 
                and incorporates an innovative pneumatic air cushion whose 
                softness can be adjusted to individual preferences in any 
                seating position.<br>
                <br>
                Further extras include a 23-inch (currently the world’s biggest) 
                in-flight entertainment screen and generous work surfaces and 
                storage facilities. In visual terms, the new seat picks up on 
                the design elements and colour scheme that have already been 
                adopted for the new SWISS Lounges to sizeable effect. Christoph 
                Franz, CEO, SWISS talks about the latest innovation and the 
                airlines plans in an exclusive interview. Excerpts <br>
                <br>
                <b>What kind of preparation went into launching SWISS First 
                Class seats?<br>
                </b>We have been working on introducing First Class seats for 
                the last two years. SWISS has been in touch with seat providers, 
                aircraft manufacturers, in-flight entertainment companies etc. 
                All this needed to be integrated into one system. We carried out 
                market research with a select group of customers to get a first 
                hand feel of their needs. <br>
                <br>
                We hope to start flying with the new seats from spring 2009 
                onwards. The first flights with the new seats are likely to fly 
                to Delhi and Shanghai. The offering will strengthen our image as 
                a premium airlines. This is helpful at a time when premium 
                demand is slipping, it would also help us to win the loyalty and 
                trust of our customers. <br>
                <br>
                <b>The airline is working on a fleet renewal programme. How much 
                are you investing and what kind of benefits do you expect from 
                this programme?<br>
                </b>SWISS is replacing its current A330-200s with new and more 
                fuel efficient A330-330s. Once all the new aircraft are in 
                service the airline will be the only one in the world to offer a 
                First Class cabin on all its long haul aircrafts. We are 
                investing $1bn on these acquisitions. SWISS is buying these 
                planes out of its own internal cash flows generated over the 
                years. From a product perspective, having a better product is an 
                advantage. A330-330s are more environment friendly, as their 
                carbon emissions are 30 per cent less than A300-200s. <br>
                <br>
                <b>What kind of a challenge does the ongoing financial crisis 
                present? Is SWISS working on a plan to the negotiate the 
                meltdown?<br>
                </b>Mastering the crisis will be a challenge for all of us. We 
                are looking at the demand on a weekly basis and are flexible as 
                far as capacity and prices are concerned.<br>
                <br>
                SWISS may redeploy its frequencies based on where the future 
                demand is expected to come from. We are trying to keep a tight 
                grip on our cost structure. The airline has introduced variable 
                compensation for its staff. If things turn sour then there will 
                be a reduction in their salaries. If things improve then they 
                will benefit. Overall the effort is to cut costs wherever we 
                can.<br>
                <br>
                I think the drop in airline demand was underestimated initially, 
                but I am positive that the airline business will survive the 
                crisis. We hired 500 new people in 2008 and expect to recruit 
                another 200 people in 2009. Aviation is a growth industry, in 
                which long term strategic decisions need to be taken. This is 
                the only way that one can survive in a highly competitive 
                market.<br>
                <br>
                <b>
                <img border="0" src="aviation1.jpg" align="right" hspace="8" vspace="6" width="176" height="204">Swiss International Airlines merger with Lufthansa in 2005 
                has worked to the mutual advantage of both the two airlines? 
                What accounts for this? </b><br>
                We have been able to maintain a very high degree of autonomy, 
                this is based on mutual respect that the two groups have for 
                each other. It is important to manage the airline in an 
                autonomous way with sufficient profits. There is a reason why we 
                have a decentralised model – this is because smaller groups tend 
                to be more dynamic. The culture is to have autonomous groups 
                with a lot of pride and learning. We are conscious that though 
                we are a part of the same group, the element of competition 
                between the two airlines should not be lost. <br>
                <br>
                Though we work as autonomous airlines we are also trying to 
                benefit from the synergies that the merger offers. For example, 
                we have a common sales centre, we buy aircraft insurance 
                together, which saves us money. The fact that a number of 
                airlines are talking to Lufthansa is because of the way SWISS 
                has been integrated into its fold.<br>
                <br>
                It is difficult for every airline in the market to restructure 
                itself as it requires cultural similarities, mutual respect and 
                working on the same level of trust. Hopefully, some of these 
                complexities will come down and the legal environment will 
                become easier, encouraging more consolidation. <br>
                <br>
                <b>Do you see a shakeout and consolidation happening across the 
                airline industry?<br>
                </b>The airline business needs consolidation, the industry has 
                been talking about it for the past 15 years but with little 
                progress, but I am sure that a lot more consolidation will take 
                place in the next few years. The successful merger of KLM and 
                Air France, Lufthansa and Swiss Air has set a good precedent. In 
                the US there is tremendous potential for consolidation between 
                domestic airlines. So the desire for consolidation is not merely 
                academic.<br>
                <br>
                During financially challenging times, companies start looking 
                for alternative ways of doing business and this should help the 
                process. In Europe, we have low consolidation and low entry 
                barriers. Thus any growth in the business gets absorbed by new 
                capacity that gets created. Overall, if you compare the US 
                airline industry with Europe you find that we have avoided some 
                of their mistakes and this will help us in future growth. </font></p></td>
