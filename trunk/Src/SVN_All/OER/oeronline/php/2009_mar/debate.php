						<b><font face="Verdana" size="1" color="#FFFCE8">Is 
                        there a case for regulating CEO compensation and 
                        benefits?</font></b></td>
  </tr>
  <tr>
                      <td height="96" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">In Oman we have strong 
                        corporate governance standards in accordance with which 
                        salaries of CEOs need to be declared. As the salaries, 
                        perks and bonuses given to CEOs in Oman are not 
                        comparable to the West, there is no justification for 
                        any regulation. Moreover, there have been no instances 
                        of overabuse of one’s privileges. For accountability 
                        though their bonuses and stock options should be linked 
                        to the profitability of the company. In the US only 20 
                        per cent of a CEO’s salary is a fixed component, the 
                        remaining being made up of bonuses, retirement plans, 
                        golden parachutes etc. CEOs are like actors, if they 
                        perform then they can command a high salary. <br>
&nbsp;</font></td>
    <td width="1" height="96" bgcolor="#BCBEC0"></td>
                      <td height="96" valign="top"> 

<p align="justify"><font face="Verdana" size="1">The modern market economy works 
best when there is transparency and accountability. To strengthen transparency, 
all aspects of CEO pay and perks should be fully disclosed on a regular basis. 
This should include airplanes, cars, golf-club memberships, bonuses, stock 
options, retirement plans and salaries – in short everything that a common-sense 
person would consider part of a CEO reward package. To strengthen 
accountability, all aspects of CEO compensation should be voted on by 
shareholders on an annual basis. What should be the cap and who decides it will 
always be subject to debate. It is not for regulators to decide what each job is 
worth. We should leave it to the market forces to decide.</font></p>

                      </td>
  </tr>
  <tr>
                      <td colspan="3" height="25" bgcolor="#D64F30">
						<b><font face="Verdana" size="1" color="#FFFCE8">Will 
                        such regulation dampen the motivation and drive of CEO’s? </font></b></td>
  </tr>
  <tr>
                      <td height="104" valign="top"> 

<p align="justify"><font face="Verdana" size="1">If the shareholders get their 
dividends and the company is doing well, in such cases a good salary and added 
perks definitely works as a motivator for CEOs to excel. We see this at all 
levels, when targets get linked to incentives it drives people to be more 
proactive and to do better. If compensations get cut then it will surely 
discourage CEOs from putting in that extra effort. In the US most companies link 
a CEOs package to profits or turnover, some companies in Oman have started doing 
this.<br>
&nbsp;</font></p>

						</td>
    <td width="1" height="104" bgcolor="#BCBEC0"></td>
                      <td height="104" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Regulations have 
                        historically dampened motivation in every sphere and 
                        will have a similar effect even for CEOs. Performance of 
                        most leading corporations have a direct correlation to 
                        the calibre of its leader and any attempt to thwart his 
                        incentives would result in restricting initiative. It is 
                        the degree of initiative that differentiates leaders and 
                        any curbs on compensation or salaries will work as an 
                        antidote to their drive.</font></td>
  </tr>
  <tr>
                      <td colspan="3" height="32" bgcolor="#697C3A"><b> <font face="Verdana" size="1" color="#FFFCE8">
                      &nbsp;In a free market economy are super achievers entitled to 
                      super salaries?</font></b></td>
  </tr>
  <tr>
                      <td height="82" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Given the stress, 
                        challenge and the tyranny of numbers that most CEOs live 
                        through, they are entitled to high salaries, if they and 
                        the companies that they lead perform well. A CEO’s 
                        salary also works as a benchmark for what other 
                        executives get paid in an organisation, so if a CEO gets 
                        paid less than it cascades down to others within an 
                        organisation. Finally, since large companies play an 
                        important role in any economy, their leaders should get 
                        a good compensation package.</font></td>
    <td width="1" height="82" bgcolor="#BCBEC0"></td>
