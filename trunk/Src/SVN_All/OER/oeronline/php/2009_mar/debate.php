						<p style="margin-left: 5; margin-right: 5" align="justify">
						<font face="Verdana" size="1">Yes. Ultimately, it is the 
                        owners of a company who must determine whether a CEO’s 
                        rewards are justified by a CEO’s performance. Regulation 
                        and putting caps is definitely not the answer though in 
                        the current world with the financial crisis looming 
                        large over large parts of the world it seems to be an 
                        understandable reaction.</font></td>
