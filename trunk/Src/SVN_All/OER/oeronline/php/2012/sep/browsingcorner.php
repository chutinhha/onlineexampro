				<img border="0" src="images/browsingcorner-sep-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3" width="140" height="270"><b>The 
				Art of War Translated into English by Thomas Cleary, gives tips 
				on how to outsmart one’s opponent so that physical battle is not 
				necessary. So much so that there are business books applying 
				lessons from this to office politics and corporate strategy. An 
				OER review</b><br>
				<br>
				The Art of War, an ancient Chinese military treatise attributed 
				to Sun Tzu, a high-ranking military general, strategist and 
				tactician, is still perhaps the most prestigious and influential 
				book of strategy in the world today, as eagerly studied in Asia 
				by modern politicians and executives as it has been by military 
				leaders since ancient times.<br>
				<br>
				Compiled more than two thousand years ago by the mysterious 
				warrior-philosopher, The Art of War is a study of the anatomy of 
				organisations in conflict. <br>
				<br>
				The book believed to have been compiled during the late Spring 
				and Autumn period or early Warring States period, is composed of 
				13 chapters, each of which is devoted to one aspect of warfare. 
				It is commonly known to be the definitive work on military 
				strategy and tactics of its time.<br>
				<br>
				It has been the most famous and influential of China’s Seven 
				Military Classics, and for the last two thousand years it 
				remained the most important military treatise in Asia, where 
				even the common people knew it by name. It has had an influence 
				on Eastern and Western military thinking, business tactics, 
				legal strategy, and beyond.<br>
				<br>
				Translated by Thomas Cleary, the English version presents the 
				classic from the point of view of its background in the great 
				spiritual tradition of Taoism, the origin of psychology, 
				science, and technology in East Asia and the source of the 
				insights into human nature that underlie this most revered of 
				handbooks for success. <br>
				<br>
				Thomas Cleary, in his translation tries to bring out the meaning 
				of the principles of strategy from standard collection of 
				commentaries on Sun Tzu’s text by eleven interpreters. In 
				addition, the translator provides an extensive introduction 
				discussing the content and background of the book.<br>
				<br>
				The book was first translated into the French language in 1772 
				by French Jesuit Jean Joseph Marie Amiot and a partial 
				translation into English was attempted by British officer 
				Everard Ferguson Calthrop in 1905. Leaders as diverse as Mao 
				Zedong, General Vo Nguyen Giap, Baron Antoine-Henri Jomini, 
				General Douglas MacArthur, and leaders of Imperial Japan have 
				drawn inspiration from the work.<br>
				<br>
				It applies to competition and conflict in general, on every 
				level from the interpersonal to the international. Its aim is 
				invincibility, victory without battle, and unassailable strength 
				through understanding of the physics, politics, and psychology 
				of conflict. <br>
				<br>
				Sun Tzu considered war as a necessary evil that must be avoided 
				whenever possible. It notes that “war is like fire; people who 
				do not lay down their arms will die by their arms. The war 
				should be fought swiftly to avoid economic losses: “No long war 
				ever profited any country: 100 victories in 100 battles is 
				simply ridiculous. Anyone who excels in defeating his enemies 
				triumphs before his enemy’s threat become real”.&#8194;</font></p>
