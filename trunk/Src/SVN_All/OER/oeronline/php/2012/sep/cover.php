						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						On the Information Superhighway</font></b><p class="style15">
				<span class="style14"><strong>The wind of ICT renaissance is 
				blowing across the world at an irrevocable pace, and Oman has 
				identified its potential as a key enabler of economic and social 
				development. But the country has to focus more on harnessing its 
				human resources and developing indigenous solutions in order to 
				emerge as a regional information powerhouse. A report by 
				Muhammed Nafie</strong></span><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-sep-12-1.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="400" height="308"></font><p align="justify" class="style13" dir="ltr">
				Information technology and telecommunications have become the 
				main engine of development in the third millennium. That is why 
				we have attached importance to producing a national strategy for 
				developing people’s potential and skills in this field. We are 
				keeping a close eye on the significant steps that are being 
				taken in this respect as we aspire to push the Sultanate towards 
				the boundaries of new knowledge…”<br>
				<br>
				– His Majesty Sultan Qaboos bin Said at the annual inauguration 
				of the Council of Oman on November 11, 2008<br>
				<br>
				One of the phenomenal aspects of the Information Communication 
				Technology (ICT) revolution the world over is the unprecedented 
				way it turns our life inside out. Unlike all other previous 
				revolutions in human history which had contributed in different 
				ways to build our civilisation, the current digital revolution 
				is transforming our life in its entirety. There is hardly anyone 
				in the world - be it laymen or people of consequence, and no 
				area of life - be it governance, education, banking, 
				manufacturing, communication, entertainment and what not- that 
				remain untouched by the sweeping influence of the tidal wave of 
				information technology, in one way or the other.<br>
				<br>
				The rapidity and ease with which the society at large adapts to 
				the latest technologies can be seen everywhere, from the Global 
				Positioning System (GPS) which guides you when you lose your way 
				while on a desert safari, to numerous personalised apps at your 
				smart phone; from a smart class room where students come with 
				MacBook and log on to KhanAcademy.org to listen to various 
				lectures and video clips while their teacher can see who is 
				watching what, to the online facilities to book your tickets and 
				check into and check out of a hotel. No technology or invention 
				in human history has assumed such a ubiquitous presence and 
				remarkably inclusive nature. While even the printing machine of 
				Johannes Guttenberg had arguably failed to address the blind and 
				the illiterate, the ICT leaves no one in the lurch.<br>
				<br>
				<b>Alluring prospects<br>
				</b>Oman was not late to wake up to this realisation as it has 
				identified the importance of Information Technology as a key 
				enabler of economic, social and community development and laid 
				down a comprehensive strategy to create a knowledge-based 
				economy in its Vision 2020. A slew of initiatives by the 
				Information Technology Authority has marked Oman’s vault into 
				the modern age of ICT. The last decade saw the number of 
				internet users in the Sultanate rising from a meagre four per 
				cent to nearly 70 per cent with an ever increasing demand for 
				broadband subscription and mobile internet. The e-Oman strategy 
				of the government seeks to bridge the digital divide and make 
				e-government services available to all the people in the 
				country. The ITA has launched a number of IT-related 
				infrastructure projects, including an e-government services 
				portal, a gateway to the services offered by government 
				departments online.<br>
				<br>
				According to Business Monitor Internationa (BMI) forecasts, the 
				total size of Oman’s IT market is estimated to be $363mn in 
				2012, up from an estimated $339mn in 2011. It forecasts a CAGR 
				of six per cent for 2012-2016. The Omani addressable software 
				market is projected by BMI at $70mn in 2012 and is expected to 
				grow at a CAGR of seven per cent over 2012-2016. In 2011, there 
				was a pickup in project flow from significant IT verticals such 
				as oil and gas and banking. The Omani IT services market is 
				expected to be worth around $96mn in 2012, accounting for about 
				27 per cent of all IT spending in the country.<br>
				<br>
				Kit Lloyd, who recently landed in the Sultanate as the new 
				country general manager of Gulf Business Machines (GBM) bringing 
				27 years of experience with IBM world over, waxes eloquent about 
				Oman’s prospects to emerge as an IT hub in the region. “Oman has 
				a great opportunity to leapfrog the rest of the region in 
				Information Technology,” he says. “The world economic forum has 
				ranked Oman 40th in world for networking access. That is a 
				remarkable achievement. The government’s focus on developing 
				skills in information technology is commendable. Knowledge Oasis 
				Muscat (KOM) is a physical embodiment of the commitment to 
				training people and it can serve as a magnet for more investment 
				by foreign companies. The establishment of a centre like KOM is 
				a great step forward.”<br>
				<br>
				<b>Inclusive governance </b><br>
				As part of its e-government initiatives, the country has 
				accelerated the adoption of ICT across the public sector in 
				order to deliver more efficient, transparent, more inclusive and 
				more sustainable government to all citizens. Highlighting Oman’s 
				notable achievements in this area was the recently published UN 
				e-government readiness survey in which the Sultanate was ranked 
				64th among 193 countries, advancing 18 positions from the 
				previous survey.<br>
				<br>
				The Sultanate has rightly understood the increasing role of 
				e-government in promoting inclusive and participatory 
				development of people. In an important step towards this 
				direction, Muscat Municipality opened in May 2012 a 
				state-of-the-art 24-hour call centre which enable city dwellers 
				to register their complaints about all municipal services by 
				dialing 1111. Be it a burned out streetlight next door or the 
				cockroach problem at home, within two minutes of receiving your 
				call, the call centre traces the number of the official 
				concerned with this particular job out of the 2,500 plus 
				employees of the Municipality and sends to him/her an SMS. For 
				each complaint the call centre gives a ticket number to the 
				caller for further follow-up and once the problem gets 
				rectified, it sends a confirmation message. This advanced call 
				centre which was developed by Modern Information Technology 
				(ITON) includes three support lines. The first line receives 
				complaints and customer care executives record the complaints 
				before passing it onto the second support line which forwards it 
				to the concerned directorate or department and follows up on the 
				process. The third support line, which represents the concerned 
				body, replies to the second line with the proposed solution or 
				action taken. The Municipality plans to bring all important 
				hotlines in the country under its contact centre wherein callers 
				will just need to dial 1111 for complaints regarding any public 
				service. <br>
				<br>
				ITON has done a similar project for the Public Authority for 
				Water and Electricity (PAWE). Whether you call for applying for 
				a new water connection, or changing the metre, the system 
				passses it onto the person/office concerned after mapping all 
				the 35 offices of the PAWE across Oman. In addition, the 
				Ministry of Housing has successfully implemented ITON’s flagship 
				solution e-Office, to clearly configure business processes, 
				leading to better process performance in terms of turnaround 
				time and quality, without compromising information security and 
				safe upkeep of digital assets. This project has enabled the 
				company to win the prestigious His Majesty’s Award for eProject 
				in 2011.<br>
				<br>
				“We are proud to be part of the digital vision of the country by 
				creating innovative platforms locally in tune with global 
				trends,” says Shiva Prasad Vakalapudi, CEO of ITON. “ITON 
				continuously contributes to develop the Omani digital society 
				and e-government through many projects concerning development of 
				the IT industry in the Sultanate and also to expand research and 
				development activities in all sectors of the national economy. 
				ITON has a deep understanding of the user community and business 
				dynamics of government organisations of this region which in 
				turn brings a lot of value and performance efficiency into the 
				organsation. ITON has 180 highly qualified and experienced 
				resources in the region and has its regional centre of 
				excellence based in Muscat”. <br>
				<br>
				ITON has been putting a lot of efforts using R&amp;D centres in 
				enhancing its platform, which is being rechristened as ITONX, to 
				scale up the adaptability of new technology and functional 
				innovations from time to time. Vakalapudi adds that ITON has 
				several national initiatives such as e-visa system 
				implementation and has more than 300 associates globally at 
				various locations like USA, Canada, Australia, Middle East, 
				Africa and India.<br>
				<br>
				Another most important e-government initiative getting underway 
				is the National Public Key Infrastructure (PKI). IMTAC is 
				providing the infrastructure solution for this project which 
				will be operational in the next year. The PKI is an advanced 
				public infrastructure solution which primarily enables 
				transactions to be digitally signed and exchanged in a very 
				secure format and this can have multiple applications in 
				banking, government and stock exchanges. Says Fareed Al Hinai, 
				vice chairman of IMTAC, “Today you do a lot of transactions via 
				manual signatures which will be completely converted to digital 
				signatures once the PKI is in place. It is part of ITA’s digital 
				society strategy. It has multiple applications such as issuing 
				certificate for housing, registration when you buy and sell 
				properties, issuing shares etc. IMTAC which set up the country’s 
				prestigious National Payment system that functions 24/7 has 
				worked on multiple projects with the ITA, banks and ministries. 
				“We have a very successful track record in implementing large 
				national level projects in the Sultanate,” adds Al Hinai.<br>
				<br>
				The Central Bank of Oman’s (CBO) OmanNet, a central automated 
				teller machine (ATM) and point of sale (POS) national switch, 
				developed by IMTAC as a local partner, is one of the most 
				sophisticated e-payment systems in the Middle East. Al Hinai 
				says, “We are proud to be part of it. Nobody in West Asia has 
				such an integrated system which enables the migration of all 
				financial institutions onto a common state-of-the-art 
				technology. The system is a showcase of the Sultanate’s adoption 
				of advanced technology and has received positive reviews 
				regarding its efficiency.”<br>
				<br>
				<b>Proving its strength</b><br>
				Oman’s capabilities to provide sophisticated digital facilities 
				were put on a real test when the country hosted the second Asian 
				Beach games. Bahwan IT did a marvelous job by providing quality 
				IT services, software and timing equipment to conduct the games 
				successfully. “It involved the planning of the entire services, 
				registration of visitors, athletes, sportspersons, officials and 
				connecting all the three venues,” says MK Janaki Raman, general 
				manager, Bahwan IT. There were 40 countries and 14 games and we 
				managed to do it so well that we were appreciated by the 
				International Olympic Committee.” Bahwan IT also handled the 
				entire procedure of the recent elections to the Majlis al Shura 
				which included the registration of voters, designing and 
				printing the ballot papers, distribution of voting equipment in 
				all the wilayats, supporting the digital counting of the votes 
				by using high-end scanners and online publication of the 
				results.<br>
				<br>
				Integrated Systems has also executed a couple of high-profile IT 
				infrastructure and network projects for the government recently. 
				“The company offers state-of-the-art technologies focused on IT 
				infrastructure, networking, security, unified communication 
				system and services and most of our products are aimed at the 
				government, corporates such as banks and public companies,” says 
				Haitham Abu Nasser, general manager/partner, Integrated Systems. 
				The company brings the latest technologies from well-known and 
				reliable vendors the world over to serve its customers.<br>
				<br>
				<b>Clearing Obstacles</b><br>
				But a moot point is how far Oman has gone to promote its own 
				indigenous software development and harness its own human 
				resources to usher in a productive and innovative IT culture. 
				Fawzi Al Harrasi, managing director, Exceed IT highlights the 
				importance of investing more on software development in order to 
				tap the full potential of the country. “Unfortunately we do not 
				have more companies who are serious about developing innovative 
				solutions unique for the country. We are more used to trading 
				products than producing solutions.”<br>
				<br>
				“We have to go beyond importing customised products from 
				principals aboard. We do not have bigger IT projects creating 
				solutions for the regional and international markets at large, 
				despite having a strong higher education system in place and a 
				lot of other advantages such as political and economic 
				stability.” Fawzi attributes this failure to various causes 
				including the lack of support from financial institutions who 
				hesitate to fund IT companies. “We lack venture capital 
				investment which is the main driver of innovation. Innovation is 
				there in people’s mind but they cannot bring it into reality 
				because it requires resources, highly skilled people, and 
				funding.” Fawzi who has recently been to Silicon Valley has seen 
				how a competitive atmosphere coupled with the support of venture 
				capitalists enables fresh graduates to come out with innovative 
				solutions. In addition, as an oil producing country we expect 
				everything to come from the government. He says that though the 
				government has a comprehensive ICT strategy, we need to be 
				faster in walking the talk. “Attracting more MNCs to operate 
				from Oman can serve as a solution; but they must transfer 
				knowledge to the locals and contribute to enhance local talent, 
				otherwise it may end up as a mere real-estate business similar 
				to what happens in Dubai Internet City.”<br>
				<br>
				Exceed Academy accords more importance to building and 
				developing national human resources. It picks up young graduates 
				from different universities in Oman and abroad and trains them 
				on various Microsoft technologies. It also gets them employed 
				with various government departments and companies such as Bank 
				Muscat and Omantel. Exceed has teamed up with Microsoft to give 
				training to young Omanis in different programmes. It has trained 
				over 200 people for capacity building and has conducted a 
				12-month programme for 60 fresh graduates out of which 55 have 
				completed the course. <br>
				<br>
				S Jayakumar, general manager of International Information 
				Technology Company (IITC) is of the opinion that though Oman is 
				adapting to new technology the industry requires a major push in 
				lowering the cost of telecommunication. “Communication cost is 
				still pretty high. That does not allow companies especially the 
				smaller entities to do a lot of consolidation works and 
				centralise the system.” He also adds that the government is 
				investing more money than it was doing before but the decision 
				making cycle is a bit slow.” He thinks that getting the right 
				resources at the right time is one of the big challenges when 
				you have a project in hand. “It is not easy to find the right 
				skillsets. That is why we have trained them a lot. People who 
				come out of the Universities are not equipped for the job 
				market. It requires some amount of effort to train them and make 
				them prepared to meet the needs of the industry. But hopefully 
				things are looking better. We are seeing more brighter and 
				committed students coming out from universities.”<br>
				<br>
				Apart from providing training to its Omani employees, IITC 
				offers training to college students. In 2001, it did a summer 
				training programme and conducted a six-month training programme 
				for 25 fresh graduates before appointing them as its employees. 
				Now the company has a lot of Omanis working as its technical 
				staff.<br>
				<br>
				Primarily a system integrator providing established solutions to 
				the customers in the Sultanate, IITC pursues all the latest 
				trends in the technology such as data centre, server 
				consolidation, network consolidation, virtualisation etc. In 
				order to address the specific requirements of the market, IITC 
				has teamed up with various international partners such as CISCO, 
				HP, Microsoft and Oracle. It also works with large system 
				integrators from various part of the world such as Wipro from 
				India for large application projects and Tech Mahindra for 
				telecom applications. It has also tied up with Finacle from 
				Infosys for banking services.<br>
				<br>
				Rahul Bhavsar, CEO, Gulf Infotech stresses the importance of 
				trusting technology to make all our ICT pursuits more effective 
				and forward looking. Oman government has done enough to promote 
				IT in the country but government organisations are reluctant to 
				accept technology. He says he would be really happy if the 
				government says that the semi government sectors can opt for 
				cloud solutions. He wonders why people are still reluctant to 
				give Internet access to the employees. “Some of them block 
				Facebook and Linkedin at offices. It’s high time we changed our 
				mindset. Recently the Khimji Ramdas group has provided internet 
				access to all its employees. But still some people think that 
				internet access will make people unproductive. But trust me 
				15-20 minute access to Internet or Facebook will increase their 
				productivity; it will make employees happier and more productive 
				and they won’t crib. If you deny access to Internet at office, 
				employees will manage to do it on their own mobile devices which 
				will take more time. YouTube is a learning technology where you 
				can learn a lot of things including sales techniques. Khan’s 
				academy is a very good example of this.” <br>
				<br>
				Sajid Ali, general manager – IT, Oman Cables Industry, stresses 
				the importance of information security function. “Many think 
				that information security is all about antivirus or firewalls,” 
				he says. “Information security is much more than that; it talks 
				about confidentiality, integrity and availability of 
				information.” He suggests a major initiative (preferably on a 
				government level) to increase awareness about the role of IT in 
				business. “As far as ITA is concerned, the work being done by 
				them in bringing a digital culture is excellent, and is well 
				appreciated, but awareness is the key. I believe most of the 
				organisations in Oman are either not aware about the existence 
				of laws on ‘Cyber Crime’ and ‘Electronic Transactions’, and even 
				if they do, they don’t know how it applies to them. I would also 
				like to appreciate the work done by Oman CERT in creating 
				awareness about information security, but again, a lot more 
				needs to be done.”<br>
				<br>
				<b>Log onto cloud</b><br>
				Cloud is one of the latest buzzwords on Oman’s IT landscape. The 
				country is making rapid headway in cloud computing. For 
				instance, Gulf IT which sells Google Apps, Google’s cloud 
				computing solutions, has notched up 100 plus customers in Oman 
				within a short span. The company has recently completed three 
				years of its operation in Oman and is looking to expand its 
				presence to UAE, Saudi Arabia, Qatar and Bahrain in the next 
				couple of years. “Today everybody is talking about cloud and 
				people have accepted the technology,” says Bhavsar. “Apart from 
				Google, now HP, IBM, Microsoft and various others are giving 
				cloud services. Initially we had to educate people about the 
				benefit of moving to cloud. When we started there were two 
				million customers globally and there were big names associated 
				with Google such as Jaguar Landover. That gave a good start and 
				the buzz was already there; and when we started the recession 
				was going and so the cloud worked out to be a cost-effective 
				solution. We got initial customers especially those who did not 
				want to spend heavily on servers and software. They moved to 
				cloud to see whether they could save money. Our first customer 
				was Muscat Pharmacy and that led us to get more references and 
				now the whole Khimji Ramdas Group is our customer. We started 
				our Indian operation last year with offices in Pune, Mumbai and 
				Ahmedabad where we build our own products and tools for Google 
				Apps.”<br>
				<br>
				Haitham also considers cloud computing as a major trend in the 
				IT industry because everyone tries to use hand-held devices such 
				as smartphones and tablet computers in their businesses.<br>
				<br>
				However like any change in technology, cloud is also facing some 
				initial resistance especially because of some concerns about its 
				security. “Cloud computing is still in the awareness stage and 
				people have not so far embraced it fully but I think the change 
				will definitely happen and people will be more willing to accept 
				it as long as their data is safe and SLAs are in place,” says Al 
				Hinai. He reveals that IMTAC is looking to partner with 
				international cloud providers such as HP to provide enhanced 
				cloud services to its customers.<br>
				<br>
				But Llyod believes that there is no point in casting aspersion 
				on the security features of cloud. He says that security concern 
				is a thing of the past as there are a number ways to make your 
				virtual system 100 per cent secure in the cloud. Look at how all 
				the giants such as Stratford Stockholm, banks, insurance 
				companies and telecos are moving onto cloud without being afraid 
				of putting their data on the cloud. Look at how other 
				organsiations in the world have transformed. Cloud opens up your 
				platform for innovation. Ten years ago you had to invest 
				millions of dollars in a large system. But today you get better 
				functionality for a few dollars. The more technology gets 
				matured the faster security concerns will disappear.” He also 
				observes that the new generation coming into business management 
				position has grown up with cloud. They are used to putting their 
				data in the public network and are aware of its benefits unlike 
				the old guards of the 1960s and 70s.<br>
				<br>
				Llyod who has run a cloud computing business in many parts of 
				the world also shares his vision about enabling the Sultanate as 
				a regional cloud service provider. “Oman has to develop its own 
				local cloud services - for Oman by Oman. I hope the government 
				will introduce local cloud services to make sure that the 
				services are offered locally,” he avers. But a major challenge 
				lies in making people switch over to new technologies as some of 
				them have heavily invested in existing infrastructure. He admits 
				that since the change of mindset is hard to come, the government 
				can play a big role by investing in new technologies.<br>
				<br>
				Although IITC was one of the first companies to introduce cloud 
				based applications in the country by bringing MessageLabs, an 
				anti-virus and anti-spam application from UK-based symantec, six 
				years ago, the company currently does not sell any cloud based 
				implementation. Says Jayakumar, “Right now cloud has not picked 
				up as a technology in Oman; but we have some private cloud 
				applications. We have centralised all our applications to a data 
				centre which is used by various branches of the IITC. That kind 
				of consolidation is happening.”<br>
				<br>
				Baby Sam, director, marketing, Al Kay’s International believes 
				that although cloud computing is relatively in its infancy in 
				the Omani IT market, it is picking up momentum quite 
				exponentially. “We expect the major businesses to soon start the 
				transition to cloud at least partially. Also as cloud computing 
				services could prove a boon to SMEs by reducing their overheads, 
				a lot of these enterprises are keen on making the move to cloud 
				based infrastructure. <br>
				<br>
				Al Kay’s International, which won the ISO 9001:2008 quality 
				system award in 2011, tied up with Microsoft and Epicor for ERP 
				practices in 2012. It has also agreements with four major 
				corporates for its ERP implementation. “We delve into everything 
				from auto ID solutions like Biometric and mobility solutions to 
				ERP and CRM products like Microsoft Dynamics, Epicor, Maxim and 
				Microsoft CRM,” says Baby Sam. “Our vertical applications 
				include campus management system, HR and payroll, retail POS, 
				hospitality management software, fixed asset tracking and even 
				customised applications to suit the specific needs of the 
				individual organisation or business. We also offer power 
				solutions like UPS, generator, stabilisers, invertors and 
				battery systems.” However, automotive, hospitality, retail, 
				manufacturing, governmental, trading and services sectors 
				comprise 80 per cent of Al Kay’s total revenues.<br>
				<br>
				<b>For a class room revolution</b><br>
				Another area where ICT is bringing about a radical shift is 
				education. The Internet has proved to be making a real 
				difference in educating children. Smart class rooms, innovative 
				teaching technologies and various digital education initiatives 
				serve to cultivate creativity and explore multiple types of 
				intelligence. Needless to mention about the access to a vast 
				amount of information and the facility to learn without any 
				barrier about time and location.<br>
				<br>
				In Oman, Nawras Business has recently linked the Ministry of 
				Education (MoE) with a fast internet connection to support every 
				state school with quicker access, better web browsing, improved 
				data connectivity and enhanced study support. Teachers, students 
				and parents visiting the main MoE educational portal are now 
				able to enjoy this web experience since the new portal 
				www.moe.gov.om went live in May 2012. In addition, over 140 
				state owned schools across the Sultanate will be using the 
				Nawras Internet Professional (NIP) service delivered via WiMAX 
				technology.<br>
				<br>
				Gulf IT provides various innovative free education services 
				through Google Apps in nine higher education institutes in Oman 
				including the University of Nizwa where there are 8,000 users. 
				“Google does not charge anything for it except for a nominal 
				deployment fee,” says Bhavsar. For educational purposes, there 
				are Google docs and Google portal. The former helps students to 
				create documents, submit assignments, share it with other 
				colleagues, work together and invite others to add to it. The 
				latter helps them build a project site, store all these 
				information and share it. There are 10 million plus students 
				using these services globally including 20,000 students in Oman. 
				Bhavsar considers this as a long term investment. “Why is Google 
				giving it free? The students of today are the businessmen of 
				tomorrow, the technology goes along with them; any student 
				coming out of University of Nizwa or Majan College is ready with 
				Google apps.”<br>
				<br>
				IMTAC also offers a mix of infrastructure and software services 
				to SQU and various other private universities such as German 
				University of Technology (GUtech). In addition, the company has 
				an education technology department which works very closely with 
				the Ministry of Manpower and vocational training colleges to 
				provide them with a wide variety of educational services such as 
				labs, educational equipment etc.<br>
				<br>
				Bahwan IT was part of the National PC Initiative (NPC) of the 
				government to give personal computers to various families, 
				schools and teachers. IITC has also worked closely with a lot of 
				education institutions such as SQU, Ministry Education, and 
				Ministry of Higher Education, to provide infrastructure 
				services, networks and other products. It has conducted 
				specialised training for faculties. In the near future, the 
				company is looking to explore areas such as smart schools in 
				Oman.<br>
				<br>
				<b>Aligning IT with business<br>
				</b>On the manufacturing front, while production is going 
				digital across the world, Oman has still a long way to go to 
				catch up with the global trend. “The manufacturing sector in 
				Oman is not adapting to the latest technologies and innovation 
				compared to other countries in the world,” says Sajid Ali. “I am 
				surprised to see that in many companies either an IT department 
				does not exist, or even if it does, it is seen as a PC and 
				printer repair shop. The idea of having an IT department which 
				is part of the corporate setup, ensures IT alignment with 
				business, and ensures that technology is used at its best to 
				help the business, but is very rare.”<br>
				<br>
				Baby Sam reveals that his company intends to develop vertical 
				solutions in the areas of manufacturing and construction as 
				these industries are expected to see a big leap in the years to 
				come. Bahwan IT also supports various manufacturing companies in 
				Sohar. It is giving the complete hardware and software services 
				to Oman Refinery.<br>
				<br>
				Sajid Ali explains that the challenges faced by manufacturing 
				companies are much more than a typical trading or service 
				organisation. “Besides having typical challenges like retaining 
				customers, increasing marketshare, maintaining competitive edge, 
				etc, we have to understand that in a manufacturing company, 
				profits are made and lost in the plant, hence additional 
				challenges such as optimising the utilisation of man, machine 
				and material comes into the picture. Additionally, since we are 
				dealing with metals, where the prices are volatile, managing 
				metal prices is an additional challenge which we face.”<br>
				<br>
				Oman Cables uses some advanced technological solutions to manage 
				these challenges. The ERP solution comprising sales, 
				procurement, warehouse, finance, quality, maintenance and 
				production management solution gives insight into what is going 
				on in the enterprise, and the accurate information it gives help 
				take timely decisions. Specific cable design software helps the 
				company in designing a well-engineered, optimum design of a 
				cable which serves the customer requirement at an optimum cost. 
				The MES (Manufacturing Execution System) solution, which is 
				currently under implementation, will give the company ‘live’ 
				insight on efficiencies of machines, work in progress etc. The 
				shop floor control solutions allow the company to capture plant 
				activities regarding an order directly at the machine level, 
				thus avoiding delay in the capture of information on the floor; 
				and hence the information available to the decision makers is 
				nearly ‘live”.<br>
				<br>
				<b>Upbeat potential<br>
				</b>As Oman is fast catching up in the ICT sweepstakes, it 
				promises big business opportunities for the companies. As 
				Vakalapudi expects, the US-Oman free trade agreement, which came 
				into effect in January 2009, has provided many direct and 
				indirect benefits for the ICT sector beyond the direct 
				relaxation of barriers to trade. However, many companies from 
				UAE and other GCC countries opening up their offices in Oman 
				have triggered some price wars in the local market. Although 
				they may not eat into the business of established players with 
				proven track record to a great extent, they are seeking to win 
				the projects by hook or by crook. For all these imponderables, 
				Oman still continues to be an attractive IT destination for 
				local, regional and international players and therefore its 
				prospects are bright to emerge as a regional powerhouse.&#8194;<br>
