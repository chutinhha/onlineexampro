						that have contributed to the Sultanate’s development and 
						modernisation with a human touch. The annual country 
						book stands out for its exhaustive analysis of various 
						sectors such as oil &amp; gas, banking, NBFCs and insurance, 
						capital markets, education, healthcare, agriculture, 
						retailing, aviation, telecom, information and 
						communication technology, real estate, manufacturing, 
						power, infrastructure, tourism etc. Each of these 
						sectors is covered in dedicated chapters highlighting 
						the strides taken by the Sultanate in these areas. <br>
						<br>
						Progress, the first annual country book in the Sultanate 
						to go online, is available in a digital version on
						<a href="http://www.progressoman.com">
						www.progressoman.com</a>. <br>
