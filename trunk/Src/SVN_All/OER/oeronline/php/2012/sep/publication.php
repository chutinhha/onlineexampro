						<br>
						The Sultanate of Oman has been a land of amazing natural 
						beauty, rich history and culture and above all, a 
						friendly society with people of great talent. Today, 
						there’s an entire generation of Omanis for whom the word 
						‘impossible’ simply does not exist. They are the 
						‘Jewels’ – a cut above the rest – who’ve gone the 
						distance to achieve their dreams and ambitions.<br>
						<br>
						Last year, Jawharat Oman (JO) profiled 40 super 
						achievers who have been front-runners in their 
						respective domains; whether they were scientists with 
						ground-breaking research or sports persons who reached 
						the pinnacle of success. This year again, we went in 
						search for the most inspiring stories and compiled a new 
						edition with 41 super achievers. Sports, science, arts, 
						fashion, technology – there are super achievers in every 
						possible sphere and JO has brought the stories of these 
						people at the forefront. The edition tracks the success 
						journey of each of these achievers, the efforts they 
						have taken to reach the pinnacle of success, the 
						difficulties that they faced and the vision to shape 
						their laurels. It explores the qualities which have made 
						them stand out.<br>
						<br>
						The 2012 edition of JO is published by United Media 
						Services (UMS). According to Alpana Roy, executive vice 
						president, UMS, “Jawharat Oman is truly a one-of-a-kind 
						publication and with the second edition I hope that it 
						continues to serve as an inspiration to all Omanis and 
						drives them to work towards their goals. In a world of 
						triumphs, the ones that are shared with others hold much 
						meaning and I have no doubt that each personal story 
						will serve to further strengthen readers’ resolves and 
						foster more inspiration in them.”<br>
