						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Strong 
						Performance</font></b><p align="center" style="margin: 10px; ">
				<img border="0" src="images/automotive-sep-12-1.jpg" width="500" height="333"><p align="justify" style="margin: 10px; ">
				<b><font face="Verdana" size="1">The newly launched Phantom 
				Series II has subtle visual refreshments with extensive 
				technology upgrades, says Geoff Briscoe, Regional Director, 
				Middle East, Africa and Latin America at Rolls-Royce Motor Cars 
				in a conversation with Akshay Bhatnagar. Excerpts</font></b><font face="Verdana" size="1"><b><br>
				<br>
				In the Middle East region, Rolls-Royce had a good year in 2011 
				and the upswing has continued in the first half of this year 
				also. How comfortable or challenging it is for you to move in as 
				the Regional Director for Middle East with such a great 
				performance by the company in the recent past?<br>
				</b>It’s definitely a great time to join the region for couple 
				of reasons. We are blessed that it is still a growing market. We 
				are up by 22 per cent this year already and the growth is not 
				going to subside as the demand by our customers is continuing to 
				grow in the region. It is a nice time to be here! We are happy 
				that we have a strong stable market here. <br>
				<br>
				<b>Bentley grew by close to 48 per cent during the same period 
				in the region. Does it mean that Rolls-Royce has not been able 
				to optimally utilise the business growth opportunities in the 
				region?</b><br>
				I don’t agree with that. Bentley has grown at a faster rate than 
				us in the region because their base is very low. We are not 
				really competing with Bentley. We sell far higher number of cars 
				compared to total Bentley sales in the region. We are very 
				strong in this market and very happy with that position. In 
				fact, it is the strongest in relation to Bentley in rest of the 
				world. <br>
				<br>
				<b>Do you think higher sales in top end luxury car segment 
				indicate strong signals of global economic recovery?</b><br>
				I wish the rebound is there for the real. In the Middle East, we 
				can definitely see it as reflected in our growth. But it is 
				quite a challenge in Europe and I’m not talking about just the 
				auto sector but other industries as well. When Europe sneezes, 
				other markets catch cold. The US is a different matter as there 
				are definite signs that the economy is emerging again and 
				starting to move. But no economist’s prediction can be trusted 
				with complete confidence. For Rolls-Royce it is very important 
				that these strong economies do well because being a real top end 
				luxury product, we very much needs these economies to do well. 
				If they are going to do well, we are going to enjoy a greater 
				success than we are achieving now. Going back to the Middle 
				East, there is a greater demand as each quarter goes by.<br>
				<br>
				<b>What percentage of your sales in the region is bespoke?</b><br>
				It is almost 100 per cent. It is difficult to find a Rolls-Royce 
				in the region that is not bespoke. In the Middle East, I always 
				use the saying that customers are exporters of Rolls-Royce’s 
				ideas. Their ideas are unique to the cars; they are unique to 
				the area. There is no doubt that our cars in the region looks 
				different due to the design combinations our customers choose. 
				The Middle East is way, way ahead of rest of the world in it.<br>
				<br>
				<b>With such a high degree of bespoke sales, what difference do 
				you find in the customers in the region in terms of taste and 
				lifestyle viz-a-viz rest of the world?</b><br>
				Naturally, we are creatures of our environment. The weather 
				patterns here are very different to rest of the world; 
				everything is bright and colourful here and it is reflected in 
				the cars as well. They are bright, colourful, strong and make a 
				statement. The trim combinations, stitching, various design 
				selections, are all driven by the customers. As I said they are 
				exporters of ideas. Rest of the world takes ideas that come from 
				the minds of the customers and dealers of the Middle East.<br>
				<br>
				<b>With such a high involvement of the customers in designing 
				the car, what is the unique role of the dealers like Al Jenaibi 
				International Automobiles in the whole sales process?</b><br>
				If I put in percentage terms it is half and half. I’m amazed by 
				the creativity of the dealers here. Before we launch a car, we 
				might ask the dealer to come up with a design for a new series. 
				And some of the themed cars they come up with are stories. They 
				are so passionate about developing a theme, developing a story. 
				We do that in any case of course. We have our designers working 
				on all sorts of things for the Middle East. But there is no 
				better invention than the inventions done here. And our dealers 
				definitely play a role here as they develop their own cars, 
				their own stories and when they are with a customer in the 
				showroom, they are so passionate because it is their own story. 
				It is not something that Rolls-Royce has come up with 
				necessarily. It is their special story and they love telling 
				them. That’s how and why they sell Rolls-Royce which makes us 
				what we are today in the region. Our dealer network plays a big, 
				big part in our success story in the Middle East.<br>
				<br>
				<b>What is the essence of the newly launched Phantom series II?<br>
				</b>As we are a low volume car company at the top end of the 
				luxury goods, we have the ability to really listen to our 
				customers and hear what they have to say to us. During the 
				launch of Phantom Series II in Oman, I’m going to meet and 
				listen to 20-30 of our customers. Our customers know more about 
				the cars than we know most of the time. They are with it most of 
				the time and so passionate about it. So they told us that they 
				don’t want a radical change in the way the car looks. They said 
				the look is stunning, stay with it; make the necessary 
				evolutionary changes and get the technology right by bringing 
				all the latest technologies in the car. We listened to our 
				customers and made the changes to come up with Phantom Series 
				II.<br>
				<br>
				<b>Can you take us through some of the key visual and mechanical 
				modifications in Series II?<br>
				</b>As I said before, sophisticated but subtle visual 
				refreshments have been introduced both on the exterior and 
				interior of the car while extensive, under-the-skin technology 
				upgrades have been introduced. On the exterior front, the 
				thoroughly modern front of Phantom Series II remains striking 
				and artistically elegant. Above the redesigned front bumpers, we 
				have introduced new, cutting-edge rectangular LED headlights 
				with curve light functionality. Phantom Series II is the first 
				car in the world to offer full LED headlights as standard 
				equipment.<br>
				<br>
				<b>In terms of looks, don’t you think the second series of 
				Phantom has gained more resemblance towards Ghost?<br>
				</b>I don’t agree with it. Phantom is the king of cars. There is 
				a gap between Phantom II and Ghost and it is intentional. They 
				are very different cars and have very different buyers. They 
				haven’t got closer. They stay exactly where they should be.<br>
				<br>
				Going back to your previous question, on the mechanical side, 
				the ultra silent, 12-cylinder engine remains unchanged and is 
				now married to a new 8-speed automatic gearbox which enhances 
				drive dynamics and maintains the serene, effortless Rolls-Royce 
				ride comfort. We have also redesigned and modernised the 
				Phantom’s driver interface. Every model in the range now 
				benefits from new driver assistance systems, such as; the 
				satellite navigation and information system, now with a larger 
				monitor for enhanced convenience and functionality. Front, rear 
				and top-view camera systems are also new additions.<br>
				<br>
				<b>What are your expectations from it in the region in term of 
				sales?<br>
				</b>Phantom has been successful in the region any way. It is 
				definitely one of the strongest markets in the world. For Series 
				II, we already have many preliminary orders at this stage which 
				is a very good sign that we are going to see a good double-digit 
				growth from the car.<br>
				<br>
				<b>You have a new design director in place. What is the brief to 
				him?<br>
				</b>I’m not in a position to answer this question. What I can 
				say is that the passion among our design team is staggering. 
				They live and breathe the brand every moment of their life. The 
				design centre is buzzing with lot of action and I’m sure there 
				will be consistency in our design strategy despite the change.<br>
				<br>
				<b>What’s the next plan of Rolls-Royce in terms of the launch of 
				a new model or a further upgrade of say Ghost?<br>
				</b>Of course, there are always new models in the future; 
				otherwise we wouldn’t have a future. The customers want to see 
				change as I said before. Our lifecycle is much longer than a 
				normal car lower down the scale. We will launch new models in 
				time. I won’t talk about them now. We are always full of 
				surprises; we want to stay that way. We are done with our 
				planning cycle for the next 5-10 years. We know exactly what we 
				are doing.<br>
				<br>
				<b>Are you working on a sportier and faster car?<br>
				</b>I think there is always a danger in trying to go one 
				particular way or the other. We are Rolls-Royce. Ghost is a 
				sportier model and Phantom was always intended to be the way it 
				is. So we’ve already done that in a way and our intention is 
				never to proliferate the brand. If we do that, we have too many 
				cars out there, we cease to be exclusive. That would be a wrong 
				way to go. I think we’ve got the right mix now. What we will do 
				in the future, you will hear about that in the coming years.</font></td>
