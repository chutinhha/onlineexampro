						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">Oil revenues set to 
						reinvigorate private sector</span><span class="style14"><br>
						<br>
						<br>
						</span></strong><b><font face="Verdana" size="1">
						<img border="0" src="http://www.oeronline.com/images/oliver_cornock.jpg" width="150" height="186" align="right" hspace="8" vspace="3">The 
						enhancement of the private sector’s role as well as 
						increasing education and skills levels can be fuelled by 
						the hydrocarbons sector for some time to come<br>
						<br>
						By Oliver Cornock</font></b><br>
						&nbsp;<p align="justify" class="style13">
						The Omani economy is enjoying something of an oil 
						revenue windfall. According to the Ministry of Finance, 
						revenues for the first six months of 2012 were $19.1bn, 
						up from $14.1bn for the corresponding period in 2011 – 
						representing a 35 per cent increase.<br>
						<br>
						This is newsworthy in itself, but it is how the 
						Sultanate is choosing to spend this welcome 
						cash-injection that reveals the government’s priorities 
						and the future course for the country. And it’s jobs 
						that are top of the agenda. Indeed, oil revenue is 
						bringing about a redoubling of efforts to curb 
						unemployment and, more specifically, a re-examination of 
						how the private sector can generate more jobs.<br>
						<br>
						As the figures indicate, the oil industry is alive and 
						well in many ways, and hydrocarbons still account for 
						around three-quarters of Oman’s budget income. And given 
						the market for oil is currently so strong, one might 
						wonder whether plans for economic diversification are 
						quite so essential.<br>
						<br>
						Certainly, the price of oil so far this year has 
						exceeded expectations; figures released from the 
						Ministry of Finance show Oman sold its oil at an average 
						price of $115 per barrel in the first five months of 
						2012. Furthermore, Oman has in many senses benefited 
						from not being a member of OPEC - its non-member oil 
						producer status gives it greater control of its revenue 
						stream, and has allowed it to take full advantage of the 
						high oil prices. Whereas OPEC has continued to closely 
						monitor production levels of its members to avoid an 
						oversupply of oil in the market (implementing an overall 
						group output limit, but stopping short of setting 
						individual country production quotas), Oman has raised 
						oil production, from 878,800 bpd in the first half of 
						2011 to 915,000 bpd over the same period in 2012. 
						Meanwhile, oil exports rose 1.9 per cent to 133.6 
						million barrels in the six-month period, according to 
						the Ministry of Oil and Gas.<br>
						<br>
						<b>Difficult to predict <br>
						</b>But this continues to mean the economy remains 
						beholden to crude oil price fluctuations. In fact, the 
						2012 budget had anticipated a deficit of $3.1bn for the 
						year, (with expenditure of $26bn and revenues of 
						$22.9bn), with the conservative assumption that oil 
						prices would hover at around $75 per barrel, according 
						to HE Darwish bin Ismael al-Balushi, Minister 
						Responsible for Financial Affairs. Oil prices may be up 
						at the moment, but they are difficult to predict, let 
						alone rely on.<br>
						<br>
						For now, the windfall revenue gains are more than 
						sufficient to cover the additional expenditure resulting 
						from the recent salary revision, which included a 
						directive from Sultan Qaboos bin Said mandating the 
						creation of 50,000 jobs, and will generate surpluses for 
						the year ahead. But the authorities are keen to ensure 
						that economic diversification – part of the Sultanate’s 
						long-stated development strategy – remains on track so 
						that in the short term they are not overly exposed when 
						and if oil prices fall, and in the longer term are 
						prepared for a future when oil resources are depleted. 
						Getting Omanis into work is central to the Sultanate’s 
						development and diversification programme to move the 
						economy towards non-hydrocarbon revenue.<br>
						<br>
						Attention is therefore turning to achieving more 
						sustainable growth, and the government has made it clear 
						that the priority will be job creation in the 
						non-hydrocarbons sector. In August this year, the 
						Ministry of Finance announced plans to spend an 
						additional $1bn from its oil windfall to create jobs for 
						its citizens over the next 12 months (although specific 
						details were not available at the time of writing). In 
						this way, the government’s recent moves have linked the 
						fortunes of the job market with the performance of oil.<br>
						<br>
						To tackle unemployment, the state knows that the public 
						sector alone cannot meet the high demand for jobs. In 
						August, the Council of Ministers urged the private 
						sector to join hands in the execution of key development 
						projects, a process that will make the private sector 
						more visible in the public eye and “help in generating 
						more employment and steer the growth wheels.” Moreover, 
						it is expected that this new drive to tap into the 
						private sector will create new opportunities for 
						public-private partnerships (PPPs), particularly in 
						areas such as housing, healthcare, electricity and 
						water, and infrastructure, which given the predicted 
						fast growth rate of the population will likely see 
						increasing demand in the years ahead.<br>
						<br>
						<b>Expanding private sector <br>
						</b>However, the success of such measures is far from 
						guaranteed, and will depend heavily on the private 
						sector meeting the government halfway. Certainly, the 
						state has done much to expand the private sector by 
						addressing the so-called “skills gap” among young 
						Omanis, introducing programmes to encourage 
						entrepreneurship and business skills as part of its 
						drive to reduce youth unemployment. The College of 
						Banking and Financial Studies, for example, has been 
						successful in preparing young Omanis to replace 
						expatriates in banking and financial institutions. 
						Indeed, the Central Bank of Oman’s has estimated that 
						more than 84 per cent of workers in the private sector 
						are expats, who send home roughly RO2.774 billion per 
						year in remittances.<br>
						<br>
						Moreover, obtaining start-up capital has also been made 
						easier; last October, for example, Oman launched its 
						first small and medium-sized enterprise (SME) Loan 
						Guarantee Programme in an effort to bridge the gap 
						between cash-rich local financiers and potential young 
						Omani entrepreneurs. The loan programme provides 80 per 
						cent guarantees for SME loans up to $650,000, with 
						interest rates ranging from 3.4 per cent to 4.3 per 
						cent, and is part of the Omani government’s “Vision 
						2020” for a vibrant and diversified private sector and a 
						generation of entrepreneurial Omanis who can serve as 
						job creators in a post-oil economy.<br>
						<br>
						<b>Concerns of young Omanis <br>
						</b>Yet skills, education and capital are only part of 
						the problem. A recent statement issued by the Council of 
						Ministers acknowledged that while there are already a 
						number of jobs available in the private sector, there is 
						reluctance on the part of young Omanis to fill these 
						posts. Shift-work, extended hours, less job security and 
						the fewer number of public holidays are some of the 
						reasons Omani citizens turn down private sector jobs. A 
						recent report by the Shura Council said that of 53,000 
						people who resigned from their private sector jobs in 
						2011 nearly half moved to work in the public sector.<br>
						<br>
						The flip side of some private sector managers’ 
						complaints about Omanis being reluctant to take up 
						existing jobs is that private businesses are not making 
						the kinds of wholesale reforms necessary if they are to 
						play a significant role in getting Omanis into work. The 
						same report by the Shura Council found that the large 
						number of resignations from the private sector reflects 
						the frustration and discontent of Omani workers due to 
						allegedly low wages amid the rising cost of living; some 
						48 per cent of private sector employees’ salaries do not 
						exceed RO200 per month. So not only must the private 
						sector expand to take advantage of the labour-intensive 
						industries the government has earmarked for growth, it 
						must also see an improvement in job offerings relative 
						to the public sector.<br>
						<br>
						Increased investment in job creation, as well as a 
						recognition of the preference for public sector 
						employment and efforts to increase the attractiveness of 
						the private sector, should go some way to addressing the 
						situation. However, there is no quick fix. The broader 
						economic development strategy the Sultanate has embarked 
						on is by its very nature long-term, and therefore it is 
						to be hoped that the enhancement of the private sector’s 
						role as well as increasing education and skills levels 
						can be fuelled by the hydrocarbons sector for some time 
						to come.&#8194;</td>
