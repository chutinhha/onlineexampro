						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Need for a paradigm shift</font></b><p class="style15">
				<b><br>
				Perform or perish</b><br>
				<br>
				<b>The Oman Debate 2011 Session 2 titled ‘Oman – At the Crossroads’ 
				raised concerns about the increasing cost of doing business, 
				dominance of family businesses and lack of employable Omanis. 
				Excerpts<br>
				</b>
				<br>
				<b><span style="font-size: 9pt">Participants – Session 2<br>
				</span></b><br>
				<b><font color="#CC3333">Dr Mohamed Abdulaziz Kalmoor</font></b><br>
				<i>CEO, Bank Sohar;</i><br>
				<b><font color="#CC3333">Ross Cormack</font></b><br>
				<i>CEO, Nawras</i><br>
				<b><font color="#CC3333">Faisal Al Lawati</font></b><br>
				<i>Executive Director, Genetco</i> <br>
				<b><font color="#CC3333">Dr Brian D P Buckley</font></b><br>
				<i>General Manager &amp; CEO, Oman LNG</i><br>
				<b><font color="#CC3333">Ti</font></b><font color="#CC3333"><b>m 
				Sebastian</b></font><br>
				<i>Moderator</i><br>
				<br>
				<b>Tim:</b> Is the private sector committed to Omanisation?<br>
				OK, we have the results: 60 per cent say it is and 40 per cent 
				say that it is not. Dr Brian Buckley, What do you think?<br>
				<b>Dr Brian:</b> One of the commitments of companies in joint ventures 
				with foreign partners is for technology transfer, skill 
				transfer, Omanisation and that is like a cradle. If you look at 
				the region, there is a need to create 60 million jobs over the 
				next ten years to meet the aspirations of young people. Oman’s 
				challenge with this 50,000 minus 20,000 people retiring every 
				year is to create around 30,000 jobs per year. So in this 
				context the need for Omanisation is urgent and very strong. <br>
				<br>
				<b>Tim:</b> How committed can the private sector be when they have only 
				15 per cent Omanisation?<br>
				<b>Dr Brian:</b> I would not like to speak on behalf of the entire 
				private sector as you have quoted a figure that I am not aware 
				of, I am only aware of my sector which is the oil and gas sector 
				and probably it is a bit privileged. The sector accounts for 80 
				per cent of the country’s income and is a very important sector. 
				In the oil and gas sector we are talking about 75 per cent of Omanisation and in some companies anywhere between 90 –95 per 
				cent Omanisation. This is a sector which is very technical, 
				requires advanced skills and is a sector where Omanisation <br>
				has worked.<br>
&nbsp;<br>
				<b>Faisal:</b> It is not a question of whether the private sector is 
				committed to Omanisation or not, but whether the right skills 
				are available for companies to employ. If I were to speak about 
				our company we are always on the lookout for Omanis, but finding 
				the right skills in the market is a challenge. <br>
				<b>Tim:</b> What would you recommend doing about that?<br>
				<br>
				<b>Faisal:</b> Training; there is a need to concentrate on a lot of 
				training. There are colleges and universities for higher skills, 
				but there is a need for a lot of lower level skills in the 
				market and that needs to be addressed. <br>
				<b>Dr Mohamed:</b> The private sector requires employees to conduct 
				work, and they would prefer to have Omanis, but there should be 
				a case where Omanis should also be inclined to take home the 
				available jobs. You mentioned a figure of 15 per cent, we need 
				to see as to which sectors and in which organisations these 15 
				per cent are; in certain sectors this figure is as high as 92 
				per cent; it is the same Omanis and it is the same private 
				sector. So the question is not whether the private sector is 
				committed or not; I presume they are committed to Omanisation. 
				Some sectors have the enablers to give what it takes to get 
				Omanis on board. And what are these, the first one is pay, which 
				entices an Omani to take up a job. Secondly there are jobs where 
				the minimum salary that is being paid is not good or attractive 
				enough for the private sector. Thirdly, there is the social 
				acceptability part of the jobs – would Omanis like to be 
				sweepers or construction workers? One should not just look at 
				statistics but go deep into it. <br>
				<br>
				<b>Ross:</b> Omanisation has been one of the secrets of our success.
				<br>
				<b>Tim:</b> Yet His Majesty asks you to do more, so in that sense it 
				cannot be that much of a success?<br>
				<br>
				<b>Ross:</b> It’s been a success because we have been in a position to 
				create employment beyond our company. Listening to the Debate 
				till now I think we need to create whole swathes and sectors 
				which can provide employment for Omanis, and small and medium 
				enterprises are going to be a focal point for this. We were 
				given very stiff Omanisation targets and this has helped our 
				success as it has helped us be closer to our customers, 
				understand our customers, whom to work with as partners and we 
				have built up a large distribution network because we had the 
				right people.<br>
				<b>Tim:</b> Right people with the right skills?<br>
				<br>
				<b>Ross:</b> Not initially, but we emphasised a lot on training. We got 
				a lot of people from outside our industry as the only place that 
				we could have got people was from our noble competition and that 
				would never have worked as we would have ended up fighting for 
				the same resources. We have some people who worked in other 
				countries and that worked well as they worked as envoys for 
				training Omanis and they moved onto other markets. <br>
				<b>Tim:</b> I would like to hear from some people who voted no or felt 
				that the private sector is not committed to Omanisation?<br>
				<br>
				<b>Audience:</b> My name is Ahmed Fahad Salem, I have been in a unique 
				position for the last one year and nine months as I am a 
				regional trainer in my company, while I was a trainer for a 
				year. One of the things that I have noticed here in Oman is the 
				lack of self motivation and it is not something that can be 
				resolved by governmental decisions, but can be resolved through 
				strict quality assurance regulations for the private and public 
				sector and this will take care of the lack of motivation. <br>
				<b>Audience:</b> My name is Hassan Ali, I think that the private sector 
				and employers are not committed to Omanisation because there are 
				no proper statistics available for Omanisation. For example, in 
				2002, there were 71,000 people employed in the private sector 
				and today it is 200,000; and if you see the number of people 
				coming out of schools and colleges it is around 70,000 to 
				80,000. The government publishes the number and percentage of 
				people who are employed, but it does not publish unemployment 
				figures. In addition, we have a problem of sponsorship. Earlier 
				an expatriate could open a company with an Omani sponsor and the 
				latter would be paid RO20-25 on the employment of every 
				expatriate per month, and because of this we do not have exact 
				unemployment numbers.<br>
				<br>
				<b>Dr Brian:</b> If I look at the pyramid of the expatriates employed 
				at senior employment levels then we may have cases of some who 
				are highly paid; but then I do not have the protection of the labour law and have a fixed contract more than that I have three 
				jobs – one is to grow the business in a professional way; second 
				is to train Omanis and third is to make myself redundant and 
				that is a serious aspiration.<br>
				<b>Tim:</b> You have not succeeded on the third one?<br>
				<br>
				<b>Dr Brian:</b> I am working on it, when we look at the other end of 
				the pyramid, in our business we require small companies to bid 
				for the workforce. They require to take on qualified workers and 
				then it is the lowest cost. So when there is a subsidy wherein 
				the expatriate labour will get in because of his lower cost, 
				then there is an issue. It reminds me of an anecdote of a 
				Scottish couple returning from India in 1949-50. They were 
				returning from India after Independence and they had earned 
				enough to bring back an Indian maid, they arrived on Monday. And 
				on Wednesday, she comes to the lady of the house and says, 
				Memsahib, memsahib, please come to the window, there is a white 
				man collecting garbage. In a way there is a lot of this 
				unskilled and semi skilled work, apart from this, there is a lot 
				of trade work for which we neither prepare nor train our people 
				for and it is honourable work that earns a good income. If you 
				see the percentage of retained income in the country if a local 
				workforce is employed then there will be a huge multiplier 
				effect in the economy. So there is a traditional and cultural 
				values that need to be approached, more so when we have a 
				country which has a young population. <br>
				<b>Tim:</b> Ross are you working on making yourself redundant and if so 
				is there a timeline?<br>
				<br>
				<b>Ross:</b> Yes, If you see how we have built our company then you can 
				see this happening visibly.<br>
				<b>Tim:</b> You are grooming an Omani successor?<br>
				<br>
				<b>Ross:</b> Yes, that is the intention. The way we do this is that we 
				have more than one potential successor. It is a competitive 
				approach. <br>
				<b>Audience:</b> (Samir Fancy, Chairman, Renaissance Services). I was 
				amazed at that figure wherein 60 per cent said that the private 
				sector is committed to Omanisation and 40 per cent said no, 
				because in reality the private sector does not operate like 
				that. The private sector sees Omanisation as a cost and a 
				problem, they do not see it as a imperative and an opportunity 
				and the gap is what needs to be filled by the government, 
				because all the issues being discussed fall into that gap.<br>
				<br>
				<b>Tim:</b> Mohammed Kalmoor, would you like to comment on that.<br>
				<b>Dr Mohamed:</b> Yes there is a cost and it comes down to the organisation and whether it will survive. The issue once again 
				comes down to the pay as to whether he is getting the right pay 
				for the job and for the private sector organisation to pay it 
				must be in a position to do competitively what it is doing. To 
				have more Omanis working we need to enlarge the private sector 
				itself, and bring it upto a higher level than what it is.
						<p class="style15">
				<br>
				<b>Tim (to Samir):</b> Do you agree with it?<br>
				<b>Samir:</b> Partially yes; the reality is a sum of what everyone has 
				said. There is a big educational process, for example if you 
				have a technical business then you are forced to employ Omanis 
				but there are none in the field. So what does one do, one takes 
				on Omanis so that one is able to get the right technical 
				expertise <br>
				from outside. <br>
				<br>
				<b>Faisal:</b> Yes I agree that the private sector is committed to Omanisation, but there is a cost. In certain cases we need to 
				have a certain percentage of Omanis in a certain job, but we are 
				not in a position to find them. We go to the ministry and ask 
				them to help us find these people and they say sorry, we do not 
				have any. There is a choice of taking Omanis from other fields 
				and then training them, but there is a cost. The point about 
				transparent statistics is important because it helps you to 
				understand how many Omanis are available per skills or region.
				<br>
				<b>Tim:</b> The next question coming up for voting is – Have the recent 
				changes in the labour law affected productivity? The results 
				have come in; 74 per cent say it has affected productivity and 
				24 per cent say no.<br>
				<br>
				<b>Ross:</b> Yes, it has affected productivity but not from a personal 
				stand point where it hit most was the SMEs, as their costs have 
				increased substantially overnight. We have not felt the impact 
				directly but the whole input price has been affected upwards as 
				a result of this. Three years back we saw inflation climbing up 
				and the government intervened with a cap on rentals, food etc, 
				and I see a lot of that returning. In an industry like ours 
				where people expect rates to come down all the time, we are 
				facing an upward pressure. <br>
				<b>Tim:</b> Is this a huge pressure when people are still reeling under 
				the impact of the global financial crisis?<br>
				<br>
				<b>Dr Mohamed:</b> No necessarily. When one talks about productivity, 
				it means the amount of work that one does in one hour. When a 
				person gets two days off and he comes back rejuvenated and they 
				can be productive in those 45 hours that they work. It varies 
				from organisation to organisation on how a company keeps up 
				motivation levels. It may however affect productivity in the 
				manufacturing sector as you require a certain number of people 
				per machine.<br>
				<b>Dr Brian:</b> There are so many factors that affect productivity at 
				a work place where the Labour Law has a relatively minor impact. 
				Secondly, you need to see the law in the context in which it has 
				evolved like the FTA with the US, requirements of the ILO and 
				the need for a structure that provides some protection for 
				workers and this is just an evolution of that. The key factor 
				that affects productivity is based on how the management manages 
				and motivates its staff. It does have an impact on small 
				business with two or three staff, the retail business and 
				restaurants, but entrepreneurs, I am sure, will find out ways to 
				get over these. <br>
				<br>
				<b>Faisal:</b> One of the things that we need to look at is the way 
				they were able to get these changes done; it was done by force; 
				because of the people’s actions and resistance the government 
				decided to increase minimum wages and bring about changes in the Labour Law. What I am worried about is that it gives people a 
				feeling that whenever they want something they can go on the 
				streets and get things done and this will certainly affect 
				productivity. <br>
				<b>Dr Brian:</b> The changes in the Labour Law and the increase in 
				minimum wages are two separate questions. The increase in 
				minimum wages has had a huge impact on the economy of this 
				country, so the cost base of everyone has increased and 
				contractors and service providers are struggling to cope with 
				this increase.<br>
&nbsp;<br>
				<b>Tim:</b> Can we hear from someone who has been badly affected by the 
				changes in the Labour Law?<br>
				<b>Audience:</b> My name is Hussain and I have a small business of 
				manufacturing safety boots for the last 15 years. The increase 
				in minimum wages and the reduction of the number of working days 
				has impacted the sustainability and profitability of our 
				business. The government looked at only benefitting the employee 
				and not the employer, while the latter play the main role in 
				running the economy. Hundreds of companies may close in the next 
				one or two years if things do not change in terms of government 
				support to the private sector.<br>
				<br>
				<b>Tim:</b> What should the government do to help out these companies?<br>
				<b>Dr Mohamed:</b> It’s not about how to relieve the burden but how to 
				increase competitiveness of the country as a whole, because if 
				these companies are not able to pay those wages then it means 
				that they are not competitive. So there is a need to restructure 
				the economy. <br>
				<br>
				<b>Tim:</b> What about the role of banks?<br>
				<b>Dr Mohamed:</b> Banks are there for the economy as a whole. If 
				anyone comes to a bank with the right project and knows what he 
				is doing, then the banks will be very happy to help them. <br>
				<br>
				<b>Tim:</b> Mr Hussain, what kind of help are you expecting from the 
				government?<br>
				<b>Hussain:</b> When these changes occurred, we formed a committee and 
				requested the government to reduce our rents, but there was no 
				response from the government. We put forward 200 proposals, but 
				we have not heard anything from the government for the last 
				eight months. <br>
				<br>
				<b>Faisal:</b> The government does take a lot of time in studying and 
				executing things and this is something that we would like to see 
				changed. While the government needs to speed up the process of 
				decision making, it also need to take the right measures. <br>
				<b>Tim:</b> In Britain whenever they want to delay something they set 
				up a Royal Commission, you will not be going in that direction.<br>
				<br>
				<b>Faisal:</b> No.<br>
				<b>Audience Question:</b> (Mohammed Ali, MD, Galfar). Oman’s economy is 
				based on government spending and there is a move to shift from 
				government spending to a more robust private sector. With the 
				increase in wages, Oman has become uncompetitive in the region 
				in the manufacturing sector. Because of the protectionism of the 
				economy the entrepreneurial spirit of the Omani youth is not 
				being nurtured, and that needs to be promoted so that we can 
				become competitive and survive.<br>
				<br>
				<b>Tim:</b> Lets go to the third question – Is the lack of employable 
				Omanis a standard excuse by the private sector? The results are 
				as follows – 49 per cent say yes and 51 per cent say no.<br>
				<b>Ross:</b> I have heard this being said in numerous sectors but in 
				the telecom sector this has not been a problem. We have 
				stunningly competent people and 87 per cent of them are Omanis. 
				Omanis are naturally entrepreneurial and if you do not have 
				Omanis in your team you are unlikely to be successful. People 
				have the right skills, but they are not prepared for the world 
				of work and they do not have the kind of expectations that I 
				wish they had like looking for the entrepreneurial opportunity 
				once they are out of school. <br>
				<br>
				<b>Faisal:</b> The way I look at it is that it differs from industry to 
				industry and for different positions. In our company when we 
				advertise for higher level jobs we get a lot of CVs, but for the 
				lower level jobs we have a lot of difficulties. <br>
				<b>Dr Brian:</b> When we advertise for jobs, we get 10-12 CVs for every 
				technical job and around 150 CVs for every non technical job. So 
				there is a mismatch in the needs of the economy and what is 
				coming out of schools. <br>
				<br>
				<b>Audience question </b>(HH Sayyid Tarik bib Shabib). The room being 
				split mid way is because one half of the people who are not 
				being employed is because of the excuse being used while for 
				others the reason is the mismatch in skill sets. This is not so 
				black and white. The other thing that I find very important is 
				the theory of a mismatch and the fact. If you look at a CV and 
				it does not meet your expectation then you do not take that 
				person on board and take things to the next step and then do 
				whatever is required to bridge that gap and in some cases you 
				need to go through a large number of people in order to create 
				that match. <br>
				<b>Tim:</b> Is there is enough desire by the private sector to employ 
				Omanis?<br>
				<br>
				<b>HH Sayyid Tarik bib Shabib:</b> If they have had a positive 
				experience, then people persist and move forward. <br>
				<br>
				<b>Tim:</b> Let’s move to the next question – Are established business 
				houses a hindrance to SME development? The results are – 56 per 
				cent think that established business houses are a deterrent to 
				SME development while 44 per cent think that they are not.<br>
				<b>Dr Mohamed:</b> This question assumes that family houses do the SME 
				business and since they are in this field they do not give a 
				chance to new entrants. We have to again look at the structure 
				of the economy, if we look at a large number of family 
				businesses they are large because they have a large number of 
				SMEs in them. They are large because they have the financial 
				means and they are able to build on the economies of scale, they 
				are able to use the resources that they have across the 
				organisation.<br>
&nbsp;<br>
				<b>Tim:</b> The question is whether there is a level playing field, 
				where in the SMEs can take on established business houses? <br>
				<b>Dr Mohamed:</b> At the end of the day SMEs are there to serve the 
				customers and end users, then if you come to the question of who 
				is going to be more competitive – the established business 
				houses or the new businesses -- the new businesses need to be 
				given a chance. This is happening only because of entrenched 
				family businesses. Or do we need to establish new family 
				businesses to come up?<br>
				<br>
				<b>Ross:</b> In Europe, Italy is SME based, China and Singapore are 
				both different economies but they are also SME based. SMEs feed 
				from the opportunities created by other companies. For example 
				travel houses feed into Oman Air and various hotels. SMEs are 
				extremely important for this economy as they are going to drive 
				the growth of the future. <br>
				<br>
				<b>Dr Brian:</b> The country has been very fortunate to have a large 
				number of established family business houses in the last 40 
				years, which is a relatively short time. The question is whether 
				they have a monopoly and the fact is that they manage well the 
				sectors in which they operate and they do rely on serving the 
				smaller businesses. They invest in the economy and encourage SMEs to develop, because it is in their interests. But SMEs have 
				to survive on their own in a competitive environment and while 
				some may succeed others will fail. <br>
				<b>Faisal:</b> In some cases it does create a difficult environment for 
				smaller businesses. You have to understand that family 
				businesses also started as SMEs. But because of the size and 
				economies of scale it becomes difficult for SMEs to start a 
				business. <br>
				<br>
				<b>Tim:</b> Does Oman need a Competition Commission to make things 
				fair?<br>
				<b>Faisal:</b> It would be a good idea when more companies come in and 
				the business environment becomes more competitive. <br>
				<br>
				<b>Dr Brian:</b> I think it is fine, while it is not for me to tell the 
				government what to do, the rules of competition should apply to 
				everyone whether they are big or small and it needs to be fair.
				<br>
				<b>Dr Mohamed:</b> My concern with this question is that it implies a 
				defeatist attitude; it reflects a zero sum game, it shows that 
				this is all that we have, and therefore we need to share; this 
				is all the employment that we have and so it needs to be Omanised and shared. This is the only economy that we have and 
				so we need to take it from one group and give it to another 
				group. It is competition that has led to family businesses reach 
				where they are because it is the survival of the fittest. So 
				when we say we have to be fair, it needs to start by creating 
				new opportunities. Thus we need to grow the economy and then 
				move onto people.<br>
&nbsp;<br>
				<b>Tim:</b> Does this mean that if you do not kill you do not eat?<br>
				<b>Dr Mohamed:</b> No, I agree that the government has a huge role to 
				play in a developing economy, to jumpstart the economy and to 
				provide all the conducive factors so that the opportunities may 
				arise. <br>
				<br>
				<b>Tim:</b> We have a question coming on Twitter – Will building brand 
				equity for made in Oman be a better alternative to managing 
				employment numbers? <br>
				<b>Faisal:</b> It is important, it will probably help SMEs also come 
				out as well, but again we come down to the issue of how it is 
				going to be done and who is going to do it, but in principle it 
				is good.<br>
&nbsp;<br>
				<b>Tim:</b> Ross Cormack, you seem to be amused by it? <br>
				<b>Ross:</b> More than amused, I am enticed by it, there is something 
				spirited about this as this is completely different from a 
				quota. A lot of people see Omanisation as a quota and never get 
				down to understanding as to why it works. If you have a brand 
				and an inspiration then it makes you behave and believe in a 
				very different way. Instead of Made in Oman, I would go in for 
				Created in Oman. I don’t think that Omanis realise the whole 
				potential, you do not need a lot of capital for having great 
				ideas. Great ideas like Google, Facebook and Baidu in China were 
				made by young guys and girls working in garages with no capital 
				at all. We should be challenging Omanis through schools by 
				telling them whether they want to go and work for one of
				the big companies or do something on their own. <br>
				<br>
				<b>Tim:</b> The next question is – Has the education system failed to 
				impart job-related skills? 89 per cent say that the education 
				system has failed to impart job related skills while 11 per cent 
				say no. <br>
				There is a related question on Twitter – Omani schools teach 120 
				days in a year, compared to the international norm of 180-200 
				days? That’s clearly a setback, why is that so?<br>
				<br>
				<b>Dr Mohamed:</b> This is clearly one of the deficiencies of the 
				system and even the number of hours within a day which are less.<br>
				<b>Faisal:</b> I agree, it all comes down to what people need in the 
				market and what people are taught to do and it all starts from 
				the school system. It is not just the amount of time spent in 
				school, but the quality of what you get. <br>
				<br>
				<b>Dr Brian:</b> The time spent in school matters. A series of schools 
				were set up in New York for the underprivileged, and children 
				who were difficult and kicked out of the state schools. These 
				schools do not give holidays, and have very long hours. The 
				percentage of children from these schools who go to Harvard and 
				Stanford is phenomenal, so it does count. The old adage – we can 
				learn, learn, learn but then we need to practice. The more time 
				you give to children the more returns you get.<br>
				<b>Audience:</b> My name is Hussain Salman. If the government thinks 
				that education cannot solve a number of problems then it is 
				mistaken. Our government has not done enough to raise awareness 
				about the importance of a working culture among Omanis. So when 
				they come to work, they do not see work as worship, but see it 
				only as a means of payment. <br>
				<br>
				<b>Dr Mohamed:</b> The education system of the whole world is not at 
				par with what is happening in the real world and there is a need 
				for a complete revamp of the education system and I am not just 
				talking about Oman. I am talking about everywhere in the world. 
				We have new technology, we know now how the brain works, we know 
				the importance of soft skills. Yet we don’t develop these in our 
				schools. <br>
				<b>Tim:</b> You still need to read, write and do arithmetic?<br>
				<br>
				<b>Dr Mohamed:</b> These are the basics, but you need to do it in the 
				right way. For example all books can be fed into an I-pad and 
				learning can be made interactive rather than being one way. <br>
				<b>Tim:</b> We need to do two more interactive questions – Is 
				public-private partnership still a distant dream in Oman? The 
				results – 71 per cent think it is a distant dream, while 29 per 
				cent think it is not.<br>
				<br>
				<b>Dr Brian:</b> I think that the country can look at separating 
				private and public in many areas, with the government being 
				there as a regulator, monitoring the rules of the game. The 
				partnership should be limited to education, the legal structure 
				that supports business, but overtime it needs to get out of 
				business and have that separation. <br>
				<b>Ross:</b> Public Private Partnership is not so formalised here, 
				meaning that there is a lot of government investment in projects 
				and out of that come a lot of opportunities for private 
				companies. For instance in our industry the government is 
				investing RO100mn in laying fibre to deliver services to homes 
				at the same time replacing the drainage system in greater 
				Muscat, so that will be a big opportunity for our industry and 
				the content industry, customer service and other kind of stores.
