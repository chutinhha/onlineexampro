						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Need for a paradigm shift</font></b><p class="style15">
				<strong><span class="style14">Oman Debate 2011 witnessed a 
				no-holds-barred discussion as government representatives, 
				private sector leaders and the public talked about the most 
				pressing issues facing the country</span></strong><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-jan01.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="497" height="291"></font><p align="justify" class="style13">
				<b>Excerpts from Session 1, titled ‘Development versus 
				stability’, which saw a spirited discussion revolving around 
				education, job creation and training</b><p align="justify" class="style13">
				<b><span style="font-size: 9pt">Panellist names – Session 1</span><br>
				<font color="#CC3333">HE Dr HE Salem ben Nasser <br>
				Al Ismaily</font></b><br>
				<i>Chairman, Public Authority for Investment Promotion &amp; Export 
				Development<br>
				</i><b><font color="#CC3333">HE Ahmed Hassan Al Dheeb</font></b><br>
				<i>Undersecretary, Ministry of Commerce and Industry<br>
				</i><font color="#CC3333"><b>Hussain Jawad</b></font><font color="#C00000"><br>
				</font><i>Chairman, W J </i><i>Towell<br>
				</i><b><font color="#CC3333">Hani Al Zubair</font></b><br>
				<i>Executive Chairman, <br>
				Zubair Automotive Group</i><br>
				<b><font color="#CC3333">Adil Taqi</font></b><font color="#CC3333"><br>
				</font><i>CEO, Muriya Tourism Company</i><br>
				<font color="#CC3333"><b>Dr Hatem Al Shanfari</b></font><br>
				<i>Faculty Member at the Department of Economics and Finance, 
				Sultan Qaboos University</i><br>
				<font color="#CC3333"><b>Tim Sebastian</b></font><br>
				<i>Moderator</i><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-jan02.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="200" height="353" align="right" hspace="4"></font><b>Tim:</b> 
				There is no doubt that as 2011 comes to an end, we can look at 
				the most far reaching and momentous events in the Arab world in 
				decades and they are still unfolding. What is the better time 
				than this to review how Oman has reacted to its own experiences 
				with the Arab Spring? I always think that the most important 
				word to define a debate is to challenge perceived wisdom and the 
				beliefs that are put around, which require us to think 
				differently in these uncertain times. I’d like you to consider 
				the statement or question, “Has Oman done enough this year to 
				meet the demands of its people?” Please vote now – The results 
				are as follows – 66 per cent say Yes, 44 per cent say No. One 
				more question: “Should the reforms stop here?” The results – 87 
				per cent say no, while 13 per cent said yes. Now we go on to the 
				panelists. Dr Salem, what do you think of the votes. There is a 
				long way to go, isn’t there?<br>
				<b>HE Dr Salem:</b> I am a bit disappointed with the first vote. I 
				don’t think we should stop here. It is a recipe for failure if 
				you stop and think you have done enough. You should continue to 
				evolve. <br>
				<br>
				<b>Tim: </b>How urgent is the situation?<br>
				<b>HE Dr Salem:</b> It’s not a matter of urgency, it’s a matter of the 
				need. When the people need it you give it to them. <br>
				<b><br>
				Tim:</b> And they certainly needed it this year…<br>
				<b>HE Dr Salem:</b> Yes they needed it.<br>
				<b><br>
				Tim:</b> Did they take you by surprise?<br>
				<b>HE Dr Salem:</b> Yes they did.<br>
				<b><br>
				Tim:</b> Should they have taken you by surprise?<br>
				<b>HE Dr Salem:</b> No they shouldn’t have. They took us by surprise, 
				we were in denial<br>
				<b><br>
				Tim:</b> Why?<br>
				<b>HE Dr Salem:</b> We didn’t expect that something like this will 
				happen in Oman. Initially, we didn’t think they were Omanis, 
				when we discovered they were Omanis, we were still in denial. 
				You know, over here, we live, eat and breathe conspiracy 
				theories. So it was always somebody else doing it. <br>
				<br>
				<b>Tim: </b>So it was all foreign inspired?<br>
				<b>HE Dr Salem: </b>That’s what we felt, but they were Omanis. 
				If you look at the city where it started in Sohar, ironically it 
				had the largest investment of $14bn and if you look at the 
				unemployment rate for the youth it is 25 per cent. That is worse 
				than West Bank.<br>
				<br>
				<b>Tim: </b>So the largest investment produced the largest 
				unemployment?<br>
				<b>HE Dr Salem: </b>Absolutely. Something is incredibly wrong.<br>
				<br>
				<b>Tim: </b>That is a huge indictment. <br>
				<b>HE Dr Salem: </b>Indeed.<br>
				<br>
				<b>Tim:</b> So where are you going to start?<br>
				<b>HE Dr Salem:</b> We started already, that was a wake-up call 
				and I thank God that it happened.<br>
				<b><br>
				Tim:</b> Ahmed Al Dheeb were you shocked as much as your 
				colleague Dr Salem?<br>
				<b>HE Ahmed:</b> I would agree in certain ways but let me just 
				explain one thing. There are reforms but everything cannot 
				happen in one year. There are plans and the results take years. 
				There were demands and these were responded to very quickly. The 
				orders given by His Majesty in response to the demands, were 
				more far reaching than what was being asked for and this process 
				will continue, but people have to realise that it is very 
				difficult for a government which is following a plan, like a 
				five-year plan for example, to do everything in one year. Let’s 
				take job creation, you cannot create 70,000 or 80,000 jobs in 
				one day or in one go.<br>
				<b><br>
				Tim:</b> But the challenge is to stay ahead of the people’s 
				demands…<br>
				<b>HE Ahmed:</b> Of course, this is why the government is 
				spending billions of rials. This is what HE Darwish said, in the 
				next five year plan, we are going to spend RO43bn so that we 
				build infrastructure, prepare for what is coming and for 
				creating jobs. His Majesty ordered for the creation of 50,000 
				jobs per year and we have already started it. The Civil Services 
				has already taken 14,000 people in 2011.<br>
				<br>
				<b>Tim: </b>But you are talking about investment. We just heard 
				that the biggest investments produced the greatest number of 
				unemployment.<br>
				<b>HE Ahmed: </b>Sure, but there are certain issues in Sohar. 
				Most people in that area are fishermen and the investments in 
				that area affected them in certain ways. A number of them gave 
				up fishing and started looking for jobs. The other issue, a 
				number of Omanis who lost their jobs in neighbouring countries 
				came back and all this happened during the same time and that’s 
				why the percentage of unemployed people increased in that area.<br>
				<br>
				<b>Tim: </b>Does that explain how the biggest investment 
				produced the greatest unemployment?<br>
				<b>HE Dr Salem:</b> It could be one of the justifications but 
				the real reason is that we started with capital intensive basic 
				industries and they don’t generate jobs and it takes time. Jobs 
				creation happens once you start going into downstream 
				industries.<br>
				<br>
				<b>Tim: </b>So are there conditions for this phenomenon to be 
				repeated?<br>
				<b>HE Dr Salem: </b>As long as the expectations of the people 
				are high, it will be repeated. Not everybody understands our 
				programmes. <br>
				<br>
				<b>Tim:</b> Hani Al Zubair, is there enough dialogue in the 
				society?<br>
				<b>Hani:</b> I think there is a lot of dialogue happening 
				between the concerned authorities and business leaders and 
				everybody else except the most important part of the society 
				which is basically the youth. I think the issue is that we take 
				it for granted that we know what the youth want. We know in our 
				household what our children want or what they are aspire for.<br>
				<br>
				<b>Tim: </b>Do you mean the ordinary people of Oman or the 
				government?<br>
				<b>Hani:</b> The government and the people who have the ability 
				to make the changes.<br>
				<br>
				<b>Tim: </b>The government is saying we know best?<br>
				<b>Hani:</b> Absolutely. I am saying as parents we know what is 
				best for our children. We do know for sure when it comes to 
				ensure their safety and security, but do we know what their 
				aspirations are? Or do we actually sit with them and find out 
				what do they want? <br>
				<b><br>
				Tim: </b>But the kids these days tell you what they want without 
				waiting for you to ask, don’t they.<br>
				<b>Hani: </b>(laughing) Times are changing, but I think we do 
				need to listen to them a little bit more. It’s no longer 
				acceptable for us to say to them, go away, sit together, decide 
				what you think you want and have a nice weekend. There has to be 
				a forum in which they sit with us and they tell us, face to 
				face, what they want. What their aspirations are. <br>
				<br>
				<b>Tim: </b>You have had youth summits here, haven’t you<br>
				<b>Hani:</b> We have had (emphasis) youth summits here. But the 
				youth summits were for youth speaking to youth within Oman, 
				outside Oman, talking about entrepreneurship or whatever. But 
				it’s not them telling us what they want, what do they need, what 
				do they aspire to going forward? We think we know what they want 
				because we are the business leaders, because we are the 
				government so we know what’s best for the country. Yes we know, 
				I think we heard some fantastic figures from His Excellency the 
				minister, that the business scenario is good, everything is fine 
				but underneath there is this boiling pot. And we need to find 
				out what’s going on, when is it going to erupt? You asked the 
				question earlier ‘were we surprised?’ Yes we were surprised. Was 
				it inevitable? Yes it was, but it is timing, we need to talk to 
				them and come up with plans that please them at the end of the 
				day.<br>
				<br>
				<b>Tim: </b>The government then has not done a very good job of 
				listening to its people. Is it going to do a better job?<br>
				<b>HE Ahmed: </b>I would say, not a very good job, but it did a 
				good job to a certain extent. Do we need to improve it, yes we 
				need to improve it. I think the government consists of the 
				citizens… we are fathers also, we have sons, we are aware, we 
				are really caring about the future of our sons.<br>
				<br>
				<b>Tim: </b>But clearly there is a reason why the people went to 
				the streets because they weren’t being listened to?<br>
				<b>HE Ahmed: </b>That I see. I say we need more, yes we do in a 
				certain way. From the people who went to the streets there are 
				lessons we have to learn. We cannot say they are totally wrong 
				and we cannot say they are totally right.<br>
				<br>
				<b>Tim: </b>By and large, you think they were justified 
				otherwise you wouldn’t have taken a number of very quick 
				measures that you did take?<br>
				<b>HE Ahmed: </b>As I said there were certain issues in which 
				they were right, that’s why the government took decisions very 
				quickly to meet their demands. That’s why we said ok, 50,000 
				jobs were announced by His Majesty, that was a very quick order. 
				There are other issues being agreed upon and I think everybody 
				knows about the issues which happened in the last 6-8 months. 
				But still there are issues that are being worked upon and they 
				have to wait till they are implemented. Things cannot be changed 
				just like a miracle. When you talk about the mega projects in 
				Sohar, the gas based industries, now we are talking about the 
				secondary projects. These downstream projects will come but not 
				in one year. It will take 2-3 years to create jobs for them 
				after that. I think the important thing is education, to educate 
				our children, we have to train them. Even the job seekers need 
				to be trained for loyalty, how to honour their jobs. Education 
				here has to emphasise on such issues.<br>
				<br>
				<b>Tim: </b>Adil Taqi, are you comforted by what you heard from 
				HE Ahmed? <br>
				<b>Adil Taqi: </b>I think the sense of urgency stems from two 
				things. One, the consequences of not doing something are very 
				negative. So if for example, the government had not met the 
				demands the way it did, it could have got out of control. So, 
				that was a driver of the sense of urgency…<br>
				<br>
				<b>Tim: </b>It did get out of control.<br>
				<b>Adil:</b> It could have got out of control more, let us say. 
				The other driver of the sense of urgency are the lost 
				opportunities. Every minute gone by is gone forever. And I 
				think, the world will not wait for us. And that cuts across 
				everything. We just got to move three notches faster, whether 
				there is a reason or not. Acting quickly is the way of life. And 
				I think that mindset needs to come into play. And it is an 
				important driver. If you take that to a macro level, if we had 
				made decisions in the years gone by when we had fantastic 
				growth, we would have achieved 60 years of growth in 41 years. 
				And that is why speed is very important. <br>
				<br>
				<b>Tim: </b>Do you think that the demands will keep coming now 
				that you have opened the flood gates? Is there a fear now that 
				all that they need to do is to go on the streets and you will 
				back down and do what they want?<br>
				<b>Adil: </b>No I think this is in a sense a natural process and 
				we have to evolve to find a new point of equilibrium. But I 
				think Hani was making an important point – it’s about engagement 
				and dialogue. We as government and citizens as HE Dheeb said, 
				the government are citizens quite rightly and I agree with that. 
				We were all surprised. The fact that we were all surprised 
				suggested that we were not in touch with the street as business 
				leaders, government officials etc. And we need to understand and 
				not become too complacent too quickly by thinking that we have 
				done enough. We also need brutal honesty, we have to be honest 
				with ourselves, face reality, take decisions and make mistakes 
				in the process. Everybody makes them. People who don’t make 
				mistakes don’t work hard enough and don’t stretch the envelope 
				far enough.<br>
				<br>
				<b>Tim: </b>How far has Oman gone in placating the people’s 
				demands? Do you think the people will go back on the streets?<br>
				<b>Dr Hatem: </b>The possibility is always there. But I think 
				the quick reaction has been quite assuring. It is necessary but 
				it’s not sufficient for getting us out of the mood that we are 
				in at the moment.<br>
				<br>
				<b>Tim: </b>Was it a knee-jerk reaction, actually?<br>
				<b>Dr Hatem: </b>It is and there will be consequences of some of 
				the decisions that we have taken to put people in jobs that are 
				not productive. It will have negative consequences. We are just 
				starting to see that almost every year we will have 50,000 job 
				seekers. We have to find jobs for them and the government cannot 
				take all of them on board. The private sector does not have that 
				much capacity either. We have a dilemma, we have been able to 
				accumulate wealth, have achieved excellent growth but that 
				growth has not been inclusive. And 50 per cent of the Omani 
				population is between the age of 15 and 29. There are about one 
				million Omanis in that age group. This is an immediate challenge 
				for the government. And the public and the youth have to be 
				assured that the government is in tune with their demands and 
				able to meet their aspirations. <br>
				<br>
				<b>Tim: </b>The government is passing the pressure primarily on 
				to the private sector<br>
				<b>Dr Hatem: </b>Private sector has very limited capacity and if 
				you keep pushing them, they will crack and this has to be well 
				balanced. The government has more responsibility to handle this 
				challenge.<br>
&nbsp;<br>
				<b>Tim: </b>How much more can the private sector take in terms 
				of pressure?<br>
				<b>Hussain: </b>Well I think there is still a lot of 
				opportunities for the private sector to grow. I think the 
				bureaucracy and the lack of coordination in the government 
				ministries is putting brakes on the private to go ahead. Today 
				we have more than 220,000 Omanis working in the private sector, 
				more than the government, which has only 140,000. So there are 
				so many opportunities.<br>
				<br>
				<b>Tim: </b>You are saying that the private sector is willing 
				but it’s just the bureaucracy which is blocking it.<br>
				<b>Hussain:</b> Yes the bureaucracy and the lack of 
				coordination…<br>
				<br>
				<b>Tim: </b>Ahmed Al Dheeb, what do you have to say?<br>
				<b>HE Ahmed:</b> Well I don’t buy that the government has to do 
				everything. <br>
				<br>
				<b>Tim: </b>But you do buy that the government is asking the 
				private sector to step up to the plate but it is blocking it 
				with bureaucracy…<br>
				<b>HE Ahmed: </b>Let me put it this way. There is a role for the 
				government and another role for the private sector. When we say 
				the role of the government, its role is to prepare the 
				legislation, the policies for the country and the private sector 
				in terms of incentives. The role of the private sector is to 
				utilise these services and run the businesses and create jobs. 
				When we say creating jobs, the government alone cannot create 
				50,000 jobs per year. <br>
				<br>
				<b>Tim: </b>What about bureaucracy?<br>
				<b>HE Ahmed: </b>Well, I don’t know where Hussain had this 
				experience but I agree that the government is not having 
				dialogue. In all the government plans and policies there are 
				committees and the private sector participates in this. <br>
				<br>
				<b>Tim: </b>So you are saying there is no bureaucracy?<br>
				<b>HE Ahmed: </b>In a certain way yes.<br>
				<br>
				<b>Tim: </b>How do you react to that Hussain? As a first 
				exercise in dialogue, it’s not very hopeful.<br>
				<b>Hussain: </b>In Oman we are always known to be really polite 
				and diplomatic. I think we have to be diplomatic and say yes but 
				in fact, it is a time when everybody’s eyes are open to see that 
				we are in a very beautiful country and there are opportunities 
				but we need more coordination and less bureaucracy. <br>
				<br>
				<b>Tim: </b>The private sector has done pretty well, it’s had 
				some very good years. Now the government wants its assistance. 
				But the private sector is complaining about bureaucracy and 
				making excuses. Now isn’t it time the private sector stood up 
				and did its duty<br>
				<b>Hani: </b>Absolutely. I don’t think it’s a one sided debate 
				or a one sided argument. The private sector does have to play 
				its role and I think the private sector in the past has not been 
				active in employing local people. We paid a lot of lip service 
				to it and we tried to find different methods and ways of doing 
				things. I will give a small example. We used to have these 
				sectoral committees to look after employment in certain sectors 
				that are now being revived which I think is a great idea. And I 
				was on the panel of two of these committees. One was in the 
				automotive sector and the other was the accounting sector. <br>
				<br>
				When we looked at the numbers we found that 50 per cent of all 
				registered foreign accountants in Oman were illiterate. 
				Everybody around the table was shocked. For me, it wasn’t such a 
				shock because our sector also played its role in those numbers. 
				The Ministry of Manpower says that you can’t have expatriate car 
				washers but we know that trying to get Omanis at that early 
				stage to wash cars all day long, 8-9 hours a day is very 
				difficult. However we could have as many accountants as possible 
				because there weren’t any in Oman and nobody was really checking 
				as to what they were doing. So there were a lot of accountants 
				washing cars. <br>
				<br>
				So people find ways to get around a system that is not working 
				for them. Has the private sector done enough? No. Are we taking 
				our part in the creation of the 50,000 jobs? Yes. But there is 
				also a limitation. Has every company that has committed itself 
				been able to fulfill its quota? I don’t think so. Not to that 
				extent that anybody would wish because you can’t find the right 
				people for the right job. We need to do more but we need to have 
				the right people at the same time. It’s no good saying you need 
				to take 20,000 more. But I need engineers or people who are at a 
				certain level. But I can only have school leavers. <br>
				<br>
				Audience: Good morning I am Salman Hussain Al Lawati. I am an 
				industrial engineer. I want to see more commitment towards 
				education. I think the government speaks about education as 
				being the most important thing currently, however, there is a 
				missing link between all the ministries. For example, we had 
				thousands of posts in the army and people left their schools and 
				colleges to join the army. So where is the priority for 
				education?<br>
				<br>
				<b>HE Dr Salem:</b> I don’t think education is only the priority 
				of the government. This is God’s priority. If you are a Muslim, 
				the first word that God said was Ikhra, read, recite, learn. So 
				investment in people is extremely important. If you have a 
				population that is not educated or trained it cannot be 
				effectively engaged in production and therefore it is very 
				important to educate people. Equally important and as I say, one 
				of the most important human rights, is the right to employment. 
				Employment is what is going to generate your livelihood. 
				Therefore the private and the public sectors have got that 
				responsibility. I think it is high time that corporate Oman 
				realised that the society in which you operate is your most 
				important stakeholder.<br>
				<b>Dr Hatem:</b> Education is the passion of the community 
				everywhere and without proper education it is difficult to go 
				forward. Education is an enabler, it does not solve the problem. 
				Obviously there are a lot of questions on the quality of 
				education. We had been channeling a huge amount of money in the 
				education sector, but its impact has not been equally effective 
				as we have been trying to address the quantity side of education 
				in the past – the amount or number of Omanis who were able to 
				enroll in higher education was proportionately very low in the 
				past. This year we had an exceptional time as about 64 per cent 
				of the high school graduates have been taken by higher education 
				institutes inside and outside Oman. The quality is another 
				challenge. We have tried to address this issue by revising the 
				curriculum and preparing the teachers better in the future but 
				it has been a challenge. And practically, no matter how good the 
				plan that we have for improving education, without committing 
				more resources into education and trying to check the quality, 
				we will not be able to realise the objectives.<br>
&nbsp;<br>
				<b>Tim: </b>So is there a political will to do that? <br>
				<b>Dr Hatem: </b>Officially yes<br>
				<br>
				<b>Tim: </b>And in practice?<br>
				<b>Dr Hatem: </b>In practice you have to go beyond the 
				commitment verbally and commit resources. Education everywhere 
				is the top priority of the government. In England, for example, 
				Tony Blair has said, education, education, education. We have 
				not even got close to that. Every country in the world which 
				want to be competitive has to address the issue of education. 
				This is an important prerequisite to be able to compete 
				internationally. We have still a way to go.<br>
&nbsp;<br>
				Audience: My name is Sridhar and I have been living here for the 
				past 30 years. In a country which has hundreds of thousands of 
				expatriates, finding jobs for about 50,000 people each year 
				should not be a difficult issue. I think, two issues have come 
				to me from the panel. One is Hani’s point. He said, I am willing 
				to take people provided I get the right talent and experience. 
				And the other point is about education. I think we need to look 
				at it in two manners. How do I solve the present problem? The 
				present problem is that we have hundreds of thousands of Omanis 
				requiring jobs. At the same time, I have hundreds and thousands 
				of expatriates who have not even qualified from school. So how 
				do we match this demand-supply situation which is available 
				within the country. We don’t want this problem to recur five 
				years from today, so what are we going to do about the education 
				where we equip our Omani nationals to take up positions in 
				future that is demanded by the private sector?<br>
				<b>HE Ahmed: </b>I totally agree about the importance of 
				education. Do we need to increase the quality of education? Yes 
				I agree we need to. <br>
				<br>
				<b>Tim: </b>Is there a political will now to do it?<br>
				<b>HE Ahmed: </b>There is, there is a will. I think we all heard 
				about how the ministry of education is working on modifying the 
				curriculum and trying to improve the level of education. And we 
				have done it several years ago, when we started with basic 
				schools, which changed the old way of education. <br>
				<br>
				<b>Tim: </b>But it wasn’t enough<br>
				<b>HE Ahmed: </b>Yes it wasn’t enough. But the world is changing 
				fast. We have to go with the changes. A few years ago, we were 
				not online, now we are on Twitter and Facebook live. So there is 
				a change and we have to go with the change. We have to go very 
				quickly I agree. The government has a lot of responsibility but 
				it is not just the government’s responsibility, we need the 
				participation of the private sector to provide good private 
				schools. Regarding the balance between unemployment and the 
				employed, there are a lot of expatriates who are below secondary 
				schools, but when it comes to Omanis business houses want 
				qualified Omanis. Why do we ask for a qualified Omani and not 
				the same from an expatriate worker? I think the reason is the 
				financial difference. The government is working on providing 
				quality education to Omanis through training institutions, 
				technical colleges and scholarships.<br>
				<br>
				<b>Tim: </b>Maybe the government is not reacting fast enough to 
				change the education or maybe the right people aren’t in 
				government?<br>
				<b>HE Ahmed: </b>Well I would say we have to move fast and 
				increase our speed a little bit…<br>
				<br>
				<b>Tim: </b>Maybe we need new faces as the priorities are 
				changing.<br>
				<b>HE Ahmed: </b>If I said yes, I would be replaced (audience 
				laugh and clap)<br>
				<br>
				<b>HE Dr Salem: </b>I don’t want to go on an overkill on the 
				education. While it is important, it’s not everything. I know 
				many countries which have invested in education and they have 
				lot of people who are educated but are unemployed. What’s 
				important is the creation of opportunities.<br>
&nbsp;<br>
				<b>Tim: </b>Is that an argument against education?<br>
				<b>HE Dr Salem: </b>No its not. It is an argument for creating 
				an environment that is conducive to do business. And that is 
				important. Equal opportunity is important. Suppose what will 
				happen if we want to marginalise people because of their gender. 
				Or if we say, women cannot work in our country. We are only 
				working on 50 per cent of the installed capacity. The gentleman 
				who has been here for the past 30 years has played a very 
				important role in the development of our economy. And I believe 
				that any person who lives in Oman, whether Omani or non-Omani, 
				has the potential to contribute to economic development. And 
				therefore, should have an equal opportunity to participate in 
				the development of the economy. <br>
				<br>
				<b>Tim: </b>You mentioned women. Are there enough opportunities 
				for women?<br>
				<b>HE Dr Salem:</b> If not, we have to create them. Let me give 
				you an example, I don’t know if you know about a guy named Abdul 
				Fatah. He was from Homs in Syria. Because he didn’t have an 
				equal opportunity in his country, he emigrated to California in 
				the US. He found a nice American lady and had a baby. 
				Unfortunately, the parents didn’t want an Arab Muslim to marry 
				their daughter. So the child was adopted. That child did not 
				finish formal education but was one of the most important 
				innovator in our time and his name was Steve Jobs. I wonder how 
				many Steve Jobs are waiting to be still discovered in Homs, 
				Syria today because of the lack of opportunity (audience claps)<br>
				<br>
				<b>Adil: </b>Very often you hear on debates and you hear it 
				elsewhere that there is mindset of a private public sector that 
				you need to work on. Most businessmen will tell you that their 
				employees are their most important resource. And I think it is 
				way beyond lip service. Any sensible businessman would say, my 
				good employees are my real assets. These are the guys who create 
				opportunities and create value. The private sector will always 
				be under countless pressure. They will have shareholders who 
				will demand to make money. But I think self interest is the 
				wider objective. Recruiting the right sort of people is what the 
				business needs. And I think we need to, in the short term, have 
				the quota systems and the percentages of Omanisation. In the 
				long term, I think if the Omani workforce is competitive as any 
				other, the employer will pick the right employee and the right 
				value creator for him. And in my view it is the competitiveness 
				that we should focus on.<br>
				<br>
				<b>Tim: </b>The best person for the job or the best Omani<br>
				<b>Adil: </b>I think the best person for the job because…<br>
				<br>
				<b>Tim: </b>Despite the quotas…<br>
				<b>Adil:</b> I think despite the quotas, because I think if you 
				have the right competitiveness, the quotas would become 
				irrelevant. <br>
				<br>
				Audience question: Good morning, my name is Lubna Al Kharusi. I 
				think we all understand what the issues are, education, 
				encouraging entrepreneurship, transparency. But my question is 
				about whether we have clear vision, plan, targets on how to 
				address these issues. If there are no measurable targets we will 
				keep talking for the next 20 years and nothing will happen. 
				(Audience claps).<br>
				<br>
				<b>Dr Hatem: </b>Well the intention is always there. And the 
				ministry has been very vocal, especially the young minister of 
				education has been trying extremely hard and I think she has 
				what it takes to move the education and to improve it 
				significantly in the future. She needs a lot of support. As I 
				said, support from the government in order to provide the 
				financial resources, support from the community to understand 
				where we are heading, and as far as measuring the education 
				performance, it is very clear, there is always high or 
				international standards and there are competitions across the 
				globe where nations compete to see how their education system is 
				faring. And when there is a lack of progress in some countries, 
				they raise a national alarm.<br>
&nbsp;<br>
				<b>Tim: </b>Dr Salem can you help the question on clear vision?<br>
				<b>HE Dr Salem: </b>I believe that the vision is probably 
				slightly blurred for the time being but it has been tuned in and 
				I totally agree with the lady’s suggestions for cleaning up 
				procedures by creating institutions, because these institutions 
				coordinate and interact with individuals, firms and the 
				government. And they have to be well functioning in terms of its 
				enforcement. However we have to slowly move from the dependence 
				on an individual to the dependence on systems. Because if we 
				empower a person, that person inherits a seat or position that 
				he has and the worst thing that could happen is that somebody in 
				public places utilises it for private benefit. If we eliminate 
				the person, we go back to transparency, good corporate 
				governance and depend upon a system rather than an individual. 
