						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Aspirational edge</strong></span><p class="style15">
				<span class="style14"><strong>Bhaskar Bhat, Managing Director, 
				Titan Industries, speaks to Mayank Singh about the evolution of 
				the brand, product extensions and how Oman proved to be a 
				learning ground for the company</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-jan12.jpg" width="300" height="451" style="border: 1px solid #808080; "></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<span class="style14"><strong>
				<br>
				</strong></span>
				<strong>How 
				has Titan evolved over the years and has the brand proposition 
				changed over the years?<br>
				</strong><br>
				We started as Titan and it continues to be our most profitable 
				brand, it is also the only brand that we sell globally. But the 
				business has changed from a single brand to multiple brands – 
				from Titan to Titan plus, Sonata, Fastrack, Nebula, Zouk and 
				Zylus. Recently we have acquired a Swiss Brand called Fabeluva. 
				Titan the flagship has managed to progress with the times. In 
				India we sell close to 1,500 varieties while in Oman we offer 
				1,000 designs. When we launched in the 1980s the Indian market 
				was closed and we were in the premium end of the market.<br>
				<br>
				Titan established itself as a good quality watch, which was 
				stylish and offered a wide variety. But as the Indian market 
				liberalised after 1992, we realised that limiting ourselves to 
				India and the same customer base would be risky. Coincidentally, 
				this was the time that we entered the Middle East market and 
				this helped the brand to evolve. Our international experience, 
				though limited to Middle East, gave us a lot of clues about what 
				it meant to compete in the international market. Our products 
				started being accepted in the Middle East and we soon acquired 
				leadership status in terms of marketshare in Oman. Globally, we 
				are one of the most attractive brands in the mid market segment. 
				This segment comprises well educated, internationally aware 
				customers who value their money and look for a combination of 
				quality, style and price. We are not as premium as the Swiss nor 
				as unknown as the Chinese. We are still aspirational and this is 
				achieved through constant product innovation. The core image of 
				Titan is that of elegance, quality and trust.<br>
				<br>
				<strong>You have gone into segmentation like Raga, Fastrack, 
				Nebula etc; why is it important for a watch maker to segment its 
				products?<br>
				</strong><br>
				Watches have today become a lifestyle product. If you want time, 
				it is available on your mobile phone, laptop, computer, TV etc. 
				Watches have become an accessory, and as an accessory it is 
				important to create a certain kind of look and brand image for 
				different segments. Apart from the mid market we address two 
				other segments – the mass market and the youth, women being a 
				sub segment within Titan. The Mass market requires exceptional 
				quality and product guarantee. Customers in this segment may not 
				be looking for a large variety, but they are definitely looking 
				for longevity and price and that’s our brand Sonata. When you 
				talk about youth there is a certain irreverence and fastrack 
				stands for playful irreverence. Fastrack started our journey 
				into accessories like sunglasses, bags, belts and wallets. We 
				are now exploring fashion footwear, helmets etc under the brand. 
				Under brand Titan we have gone into eyewear. We have a chain of 
				185 eyewear stores in India. This is the third business that we 
				have entered after watches and jewellery. Overall we have 380 
				Titan stores in India.<br>
				<br>
				<strong>Have Titan’s Edge, which was marketed as the slimmest 
				watch in the world and HTSE as helped strengthen your innovative 
				image? How important is it to constantly showcase the brand’s 
				technological strength?<br>
				</strong><br>
				The Edge initiative has not only given us stature but real 
				business as well. We did Edge initially to promote Titan as a 
				brand that is at the forefront of technology, but later it 
				became a full-fledged business proposition. We have priced the 
				product very reasonably as we wanted to popularise that 
				technology. The same is true for HTSE, which is a watch which 
				works on light energy.<br>
				<br>
				<strong><br>
				Do you invest a certain part of your turnover in R&amp;D?</strong><br>
				<br>
				It is not a conscious investment of one or two per cent, but we 
				have a separate team that pursues R&amp;D and is called innovedge. 
				The team comprises 17 people who work on products that will come 
				three to five years from now and that’s our investment in R&amp;D. 
				In our business you need not to invest in a high tech R&amp;D labs 
				but in people’s brains. For example, we are working on a product 
				in which you can download and change the dials. We also have a 
				full fledged design studio that works on coming up with new 
				designs.<br>
				<br>
				<strong>Where do you have your manufacturing facilities and what 
				kind of capacities do you operate on? What is your expected 
				turnover for financial year 2011? </strong><br>
				<br>
				Close to 95 per cent of our watches are assembled in India. Out 
				of the 16.5mn watches that we manufacture annually, three 
				million is made in Hosur in the state of Tamil Nadu; our other 
				plants are based in Uttarakhand in Roorkee, Dehradun, Pantnagar. 
				The company’s turnover is expected to be $2bn dollars this year. 
				We have 25 per cent marketshare in India by volume and 40 per 
				cent by value. <br>
				<strong><br>
				How many countries do you have a presence in and how much do 
				your international operations contribute to your business?</strong><br>
				<br>
				&nbsp;Our international operations contribute around eight per 
				cent of the watch business which is around $25mn. We export to 
				27 countries, primarily in Asia, Middle East and Far East. We 
				have a presence in Africa with South Africa being a very 
				strategic market.<br>
				<br>
				<strong>Oman was one of the first markets that Titan forayed 
				into internationally – how has your experience been here?<br>
				</strong><br>
				Oman has been one of our most satisfying markets; we achieved 
				number one marketshare status quite early and we have been the 
				market leader for years now. We operate in Oman through our 
				partner Bahwan Electronics. Oman taught us the quality-price 
				equation, in India as we were market leaders it gave us the 
				luxury of pricing ourselves higher and higher each year. When we 
				launched in Oman, we knew that we were competing against 
				Japanese and other faceless brands and it sharpened our wits. 
				This helped us in dealing with competition in India when the 
				market opened up in the late 1990s. On the operations side we 
				learnt about deliveries – that is aligning our entire production 
				system to what the market wants from companies like Seiko that 
				does this extremely well. From a design standpoint Oman showed 
				us what others were offering, the latest trends in terms of 
				product technology. The aggressiveness with which brands vied 
				for showcasing the product, visual merchandising and getting 
				shelf space in the Sultanate proved to be of great help for us. 
				Finally, the market showed us the importance of the retailer.<br>
				<br>
				<strong>As international brands set up shop in India, will this 
				take away your aspirational edge?<br>
				</strong><br>
				We have two big initiatives to negotiate this trend – we have 
				opened a chain called Helios in which we market international 
				brands like Movado, Roamer, Emporio, Armani, Fossil, Guess, 
				Tommy Hilfiger, FCUK, Casio, Seiko, Citizen etc including our 
				own. We have 14 Helios stores which are up and running. This is 
				a retailing effort to participate in the growth of international 
				brands. Secondly, our own play in the high end of the market; it 
				may not be as high as some of the international brands but 
				through Zylus, Nebula the gold watch and now Fabluva we are 
				positioning ourselves in the high end of the market.<br>
				<br>
				<strong>Aren’t you being opportunistic by doing Helios, while 
				you have an opportunity to build up Titan itself?<br>
				</strong><br>
				Every brand has a price band and you cannot go very high or very 
				low from it. A Rolex sells for INR2.5 lakh and it would be 
				foolish for a Titan to sell at that price, but there is a market 
				at the high end so Titan is pushing the price point up slowly. 
				We have automatic watches at INR30,000 ($569) and we will go 
				upto INR50,000 ($948). We have just launched a INR3.5 lakh 
				($6637) Nebula which is called the Palace Collection and is 
				based on various palaces in India and stands for Indian luxury. </td>
