				<b>
				<font face="Verdana" size="4" color="#C00000">Engines of growth</font></b><p align="justify" class="style13">
				<font face="Verdana" size="1">I was at Ernst&amp;Young’s Executive Management Development 
				Programme for Omani Nationals recently. The results of a study 
				done by E&amp;Y on the global trends that are changing the world of 
				business were presented at the session. As we enter 2012, it may 
				be a good time to look at some of these factors impacting the 
				business landscape. <br>
				<br>
				<b>The rise of emerging markets:</b> They will become equal 
				competitors with mature markets and command more economic and 
				political power. Each of these markets have their specific needs 
				and mature-market companies will need to customise their 
				products and services to cater to customers in emerging 
				economies. <br>
				<br>
				<b>An increasing focus on resource efficiency and climate 
				change:</b> World over, the pace of legislation and policy 
				initiatives focused on green issues is picking up and 
				organisational responses to resource efficiency and 
				sustainability efforts are becoming a deciding factor in 
				business performance.<br>
				<br>
				<b>The financial landscape is undergoing fundamental 
				transformation:</b> The financial sector’s recovery will be 
				marked by heightened regulation and restructuring of the 
				industry. With governments owning stakes in many financial 
				services firms, its role and influence in the sector is bound to 
				grow.<br>
				<br>
				<b>Increased role of government in the private sector:</b> 
				Following the global financial crisis, the governments’ 
				involvement with private industry will be closer. They will also 
				be faced with the onerous choice of balancing between the needs 
				of global competition and supporting domestic economies. <br>
				<br>
				<b>The evolution of technology:</b> Sophisticated analytical 
				tools will give businesses an unprecedented competitive edge. 
				New digital platforms will reshape entire industries and, by 
				leapfrogging technologies, emerging markets will be able to 
				access productivity-enhancing technologies at lower cost.<br>
				<br>
				The common thematic thread running through these trends can be 
				summed up in one word – change. The world is going through 
				unprecedented change and the winners of tomorrow will be those 
				who can negotiate and harness this process.</font></td>
