				<br>
				ICT Report 2011-12 Bahrain’ published by United Media Services 
				was recently unveiled by Basil Al Arrayed, 
				director-communications &amp; consumer affairs, Telecommunications 
				Regulatory Authority (TRA), Bahrain. <br>
				<br>
				ICT Report 2011-12 is the first of its kind publication in the 
				Kingdom of Bahrain. “The maiden edition of ICT Report Bahrain is 
				a handy comprehensive reference dossier to get an overview as 
				well as complete and latest details of the telecom and IT 
				sectors, and the major ICT companies operating in Bahrain. The 
				bilingual publication with a print run of 8,500 copies will be 
				distributed to key stakeholders in the ICT industry, relevant 
				top managers in the public and private sectors, apex trade 
				bodies and trade missions in the region and embassies apart from 
				being available at the leading book stores,” says Vinod Thangoor, 
				regional manager, United Media Services.<br>
				<br>
				The publication showcases the vision for the ICT sector in the 
				long term economic development plans in the country, interviews 
				with top industry leaders, analysis from domain experts, reports 
				on key sub-sectors within the ICT industry, etc. “We recognise 
				the need to make a thorough assessment of the future 
				telecommunication needs of Bahrain so as to ensure appropriate 
				actions are taken to achieve competitive broadband communication 
				services, at internationally competitive prices, to support 
				economic and social goals as outlined in the government policy. 
				The assessment will result in a national broadband strategy for 
				Bahrain based on the predicted future needs and requirements of 
				consumers and businesses in the Kingdom,” states Dr Mohammed 
				Ahmed Al Amer, chairman of TRA, Bahrain in an interview <br>
				with ICT Report.</td>
