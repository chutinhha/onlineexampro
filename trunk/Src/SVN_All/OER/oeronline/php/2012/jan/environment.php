				Leaving a green footprint</font></b><p align="justify" style="margin: 3px 10px"><b><font face="Verdana" size="2">
                		<br>
						Registration 
				has started for the third edition of Oman Green Awards, Oman’s 
				first environmental award to honour outstanding environmental 
				vision, endeavours and achievements of corporates and 
				individuals<br>
                </font></b>
				<font face="Verdana" size="1">
				<br>
                Environment conservation continues to dominate all our 
				discourses on holistic development and sustainable growth, as it 
				can no longer be outside the purview of any discussion about the 
				sustenance of human life on the Earth. As the world population 
				increases, the demand for food, energy, and luxuries has 
				increased manifold. The first casualty of catering to the 
				burgeoning demands of the increasing urban populace is the 
				environment. Therefore, creating a green economy has to top the 
				agenda of the corporates, individuals and organisations, and 
				every company needs to contribute its mite to protect and 
				restore what is left of the Earth and environment.<br>
				<br>
				Only an enhanced awareness campaign will stir interest and wake 
				up people from general inaction and minimal activism to positive 
				environmental goals. Globally and regionally, environmental 
				awards in governmental, corporate and other sectors have been 
				evolving for several years. Several organisations including 
				corporate houses, institutions, schools etc. are involved in 
				relentless efforts to protect and preserve the environment. But 
				the lack of an umbrella organisation that will bring these 
				players together and facilitate better coordination and exchange 
				of ideas creates an inherent lacuna in the system. It was from 
				such a perceived need that the idea of Oman Green Awards (OGA) 
				was conceived, and over the last three years it has evolved as a 
				significant step to create and increase environmental awareness, 
				as these will also lead towards greater action in the long term, 
				both at a community and decision making levels.<br>
				<br>
				OGA has played a significant role in making the corporates, 
				individuals and organisations in the country more serious about 
				building environmental sustainability into their business 
				practices. As the country’s first Environmental Awards, it has 
				been created to awaken the eco-consciousness in every citizen 
				and resident. It is aimed at honouring outstanding environmental 
				vision, endeavours and achievements of corporate and individuals 
				who matter most as they strive in this environmental cause. The 
				ultimate aim of the awards is to motivate behavioural change and 
				increase awareness in relation to the protection and 
				preservation of our environment. OGA provides green groups a 
				platform to create awareness, bring about concerted action 
				through exchange of ideas, and propel others to come forth and 
				participate in this social cause.<br>
				<br>
				The OGA is the ultimate recognition for companies, institutions 
				and individuals in Oman, who demonstrate a commitment towards 
				responsible consumption and sustainable growth. The award 
				honours and celebrates their vision in leading the way to a 
				cleaner, greener tomorrow. The last two editions of the OGA were 
				a resounding success in meeting its stated objectives. The OGA 
				has invited entries for the third edition of the awards which 
				will be announced in June, 2012. It is a unique opportunity to 
				be part of a national platform to honour and appreciate 
				outstanding environmental vision, endeavors, initiatives and 
				achievements in the country. The nomination form must only be 
				completed and submitted online at
				<a href="http://www.omangreenawards.com">www.omangreenawards.com</a>. 
				Any other form of nomination swill <br>
				not be considered.<br>
