						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>New vistas of growth</strong></span><p class="style15">
				<span class="style14"><strong>The Sultanate is all set to 
				embrace Islamic finance as the Central Bank of Oman is expected 
				to issue a full-fledged Islamic Banking Regulatory Framework 
				(IBRF) very soon. Khalid Yousaf, Director, Islamic Finance 
				Advisory Services, KPMG Oman, observes that by the end of 2015 
				Islamic finance assets will achieve roughly 20 per cent of the 
				total banking and insurance assets in the country. He urges 
				Islamic finance practitioners and scholars to invest more in 
				research and innovation to satisfy the increasing market 
				appetite. Excerpts from an interview with Muhammed Nafie<br>
&nbsp;</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face-oct-12-1.jpg" style="border: 1px solid #808080; " width="400" height="377"></font><p align="justify" class="style13" dir="ltr">
				<br>
				<b>How do you see Islamic finance/ Islamic banking shape up in 
				Oman in the coming years?</b><br>
				The landscape for Islamic finance has been carefully and 
				methodically planned in the Sultanate. Whereas a robust legal 
				environment exists already, a comprehensive Islamic Banking 
				Regulatory Framework (IBRF) has been designed by the Central 
				Bank of Oman (CBO) to address the regulatory requirements of 
				full-fledged Islamic banks and Islamic windows; another set of 
				regulatory requirements address Takaful and Sukuk by Capital 
				Markets Authority (CMA); and changes to tax regime to ensure 
				that Islamic banking products and services are not treated less 
				favourably have been undertaken by the Secretary General of 
				Taxation (SGT). This architecture of strong pillars gives the 
				Islamic finance industry an enduring foundation which will 
				ensure that the industry will build and flourish in a 
				disciplined manner. The industry however, is not likely to 
				follow the “big bang” explosive growth model. Instead it is 
				anticipated that a steady and moderate growth will be the 
				hallmark of Islamic finance industry in Oman. In my opinion, the 
				Islamic finance assets will achieve roughly 20 per cent of total 
				banking and Takaful assets by the end of 2015 in the country. 
				The turning point will be when the current conventional banks 
				and insurance companies will start converting into full-fledged 
				Islamic banks and Takaful companies to meet market demand. 
				Subsequently, Islamic assets will take the dominant share in 
				both banking and insurance sectors.<br>
				<br>
				<b>What will be Islamic banks’ potential for contributing to 
				Oman’s economic development in the long run? What kind of 
				business opportunity will it convert into?</b><br>
				Islamic banks are likely to attract a huge deposit base from 
				retail, SME, corporate and public sectors, giving them 
				sufficient liquidity to finance the “capital-hungry” projects 
				and sectors. Thus Islamic banks will bridge the gap and provide 
				capital flows necessary for steady growth of Oman’s economy. 
				Oman’s economy with GDP already approaching $70bn has 
				commitments to major infrastructure projects like the new Muscat 
				International Airport, Gas-fired power generation and road 
				network projects costing over $30bn. These projects will require 
				financing and Islamic banks can take the lead in 
				private-public-partnership (PPP) initiatives to fund 
				asset-backed transactions. Further, the markets and public 
				at-large are displaying signs of preference for Islamic products 
				and services and Islamic banks will offer retail, corporate and 
				treasury products to their customer base. Although some of the 
				customers may just be converting from conventional to Islamic, 
				it is likely that a large segment of retail, SME and corporate 
				customer base which has henceforth shunned conventional banking 
				for lack of availability of Islamic products and services will 
				take the role of new contributors to the banking sector. Their 
				addition will provide a significant boost to Oman’s economy 
				through the growth of banking assets, which in turn will boost 
				the money supply through M1 and M2 factors.<br>
				<br>
				<b>The CBO is expected to announce soon a full-fledged 
				regulatory framework for Islamic banks in the Sultanate. Since 
				there are different methodologies to follow, according to you 
				what will be the best regulatory regime for Oman?</b><br>
				There are currently three regulatory regime models practiced in 
				countries offering Islamic finance products and services. The 
				first being the “purist” model practiced by Sudan and Iran which 
				has no room for conventional banking and the banking laws and 
				regulations address exclusively the regulatory requirements for 
				Islamic banks and insurance companies only. The second model 
				offers the “parallel” laws and regulations for both conventional 
				and Islamic banking to co-exist, practiced by Malaysia, 
				Indonesia, Pakistan, UAE and now Oman. The third model which is 
				most prevalent across the globe follows the principle of 
				“financial equivalency” whereby Islamic banking products and 
				services are measured on the same legal and regulatory scale as 
				their conventional counterpart. The Western world, Australia, 
				Singapore, Hong Kong, South Korea, most other Islamic or secular 
				countries with majority Muslim population practice this model.
				<br>
				<br>
				Draft Islamic Banking Regulatory Framework (IBRF) is a 
				comprehensive set of rules and regulations for Islamic banks and 
				windows in Oman. Being the latest addition to the list of 
				countries offering Islamic banking, Oman has the advantage of 
				evaluating and comparing similar regulations around the world 
				and cherry-pick the best-practices. The draft IBRF which may 
				still be subject to modification, comprehensively covers the 
				licensing requirements; general obligations &amp; governance; 
				accounting standards &amp; audit reports; supervisory powers &amp; 
				control; capital adequacy; credit risk; market risk; operational 
				risk; liquidity risk and other pertinent operational 
				requirements. Although these rules and regulations still form 
				part of the overall banking laws in Oman, they are unique to 
				Islamic banks and window operations. Their robust and 
				comprehensive structure will not only give a powerful 
				supervisory and monitoring tool in the hands of CBO but also 
				give confidence and trust in the minds of general public.<br>
				<br>
				<b>Islamic banks have bucked the global financial meltdown to a 
				large extent as it did not have as much exposure to collateral 
				debt obligations and real estate as conventional banks. Is this 
				a major plus for it?<br>
				</b>It is true that post global financial meltdown, Islamic 
				banks did not feel the same pain as the major conventional 
				financial institutions around the world. This is largely 
				attributable to the relatively small size of Islamic banks on 
				the global scale as well as the fact that unlike conventional 
				banks which were highly-leveraged (some had assets in excess of 
				100 times their equity capital), Islamic banks either had zero 
				or very low leverage. As a result, the loan/deposit ratios in 
				Islamic banks were very low compared to conventional banks. 
				Global financial meltdown created severe liquidity problems for 
				conventional banks because their assets lost value suddenly 
				while creditors/depositors demanded payments upon maturity. 
				Consequently, their respective governments had to bail them out 
				financially. Islamic Banks also suffered some pain as their real 
				estate portfolios lost value, but having little or no leverage 
				saved them from the scale of liquidity crunch which crushed 
				their counterparts.<br>
				<br>
				<b>Two full-fledged Islamic banks and the Islamic windows of a 
				number of conventional banks are about to start operation in 
				Oman. Will it be difficult for them to find enough experts at 
				their Sharia boards?<br>
				</b>This is the crux of problems facing Islamic Finance industry 
				across the world, but even more so in Oman. CBO’s draft IBRF 
				requires every full-fledged Islamic bank or Islamic window of a 
				conventional bank to have a Sharia’ supervisory board consisting 
				of a minimum of three Sharia’ scholars. The “Fit &amp; Proper” 
				criteria lay down strict qualifications and experience 
				requirements; each Sharia’ scholar can have a maximum tenor of 
				three years initially with a maximum of two consecutive terms. 
				Also, a Sharia’ scholar cannot be a member of more than two 
				Islamic banks or a maximum of four Islamic financial 
				institutions like Takaful, asset manager or fund manager. 
				Islamic finance industry has traditionally faced a paucity of 
				qualified Sharia’ scholars, hence most noted Sharia’ scholars 
				internationally sit on the Sharia’ supervisory boards of dozens 
				of Islamic financial institutions. Islamic finance is new to 
				Oman and the shortage of Sharia’ scholars will be felt even more 
				acutely here than elsewhere. The solution lies not only in 
				easing the regulatory requirements, but also making efforts at 
				national level to train and develop Sharia’ scholars to meet the 
				burgeoning demand of Islamic finance industry. <br>
				<br>
				<b>How feasible will it be for conventional banks to offer 
				Islamic banking services?<br>
				</b>Conventional banks have been quick in announcing the launch 
				of Islamic window operations both for aggressive and defensive 
				reasons. Given the robust demand for Islamic banking products 
				and services, conventional banks want to gain their share in 
				this new market as quickly as possible. At the same time, there 
				are concerns that if conventional banks do not take immediate 
				steps, they may start losing their customers to Islamic banks 
				and windows. The conventional banks, therefore, wish to maintain 
				their overall market share or improve on it. Through the opening 
				of Islamic windows they hope not only to retain their loyal 
				customers, but also attract customers from other banks. It is 
				foreseen that in the early stages of development, competition 
				between Islamic banks and windows will be quite intense and some 
				banks or windows may even become “loss leaders” to capture the 
				target market share.<br>
				<br>
				Since conventional banks are likely to lose some customers, if 
				they do not offer Islamic banking services through window 
				operations, their positioning through Islamic window is to 
				counter the threat of losing customers to other competitors. 
				Opening an Islamic window operation therefore is not only a 
				“defensive” strategy but also an economic one because it 
				requires less capital while sharing a common cost base with 
				additional business. This strategy, therefore, compares 
				favourably against establishing a full-fledged Islamic banking 
				subsidiary. Given these cost advantages, conventional banks will 
				have the capacity to offer their products and services at lower 
				prices than full-fledged Islamic banks. They are also expected 
				to leverage their existing brand image in the market to address 
				new target Islamic banking customers.<br>
				<br>
				<b>What are the major challenges faced by Islamic banks all over 
				the world? <br>
				</b>Islamic finance is a baby when compared to conventional 
				financial system. Whereas the conventional financial system has 
				been in existence for 300+ years, Islamic finance in its present 
				form has been in existence for just over 30 years. A lack of 
				understanding of its underlying Sharia’ principles by 
				governments, regulators, industry and public at large are the 
				biggest problems facing the industry today.<br>
				<br>
				There are also skeptics who question the strict compliance with 
				Sharia’ principles and consider Islamic finance as a wrap around 
				the conventional financial products. The fact remains that being 
				fledgling, the industry has successfully made the existing 
				financial products and services “Sharia’-compliant” but will 
				take some more time to develop and introduce exclusively 
				“Sharia’-based” products and services. Hence, Islamic finance is 
				currently a part of the whole financial system, rather than 
				being a parallel financial system. The challenge for both 
				Islamic finance practitioners and Sharia’ scholars is to 
				continue to invest in research and innovation to satisfy 
				increasing market appetite. <br>
				<br>
				<b>How will Islamic banking be appealing to non-Muslims in Oman?<br>
				</b>Islamic banking products and services are not targeted at 
				Muslims only. They offer all features of “ethical” banking plus 
				more. Whereas “ethical” banking shuns alcohol, adult 
				entertainment, gambling, environment pollution and weapons of 
				mass destruction, Islamic banking goes a step further in 
				shunning conventional “riba-based” banking and financial 
				services as well. Non-Muslims seeking ethical banking products 
				are therefore attracted to Islamic banking products and 
				services. In Malaysia for instance, 35 per cent end-users of 
				Islamic banking products and services are non-Muslim Chinese 
				population seeking ethical products which meet its financial 
				criteria. Also, due to the nature of Sharia’ technique applied 
				in house, car or other loans, the terms and conditions may be 
				more acceptable to non-Muslims seeking those peculiar terms.<br>
				<br>
				<b>In your capacity as the director of Islamic finance advisory 
				services at KPMG, how are you looking to contribute to Islamic 
				banking in Oman during its gestation period?<br>
				</b>KPMG wants to have a sustainable presence in Oman by being 
				recognized as a quality service provider in the field of Islamic 
				finance. We wish to demonstrate our continuing commitment to the 
				country. This is why resources have been allocated and an 
				Oman-based director has been appointed. <br>
				<br>
				Our mission going forward will be to promote the growth of 
				Islamic finance through education, training &amp; development, 
				raising public and industry awareness, providing advisory 
				services to clients in establishing new Islamic banks, Islamic 
				windows, Takaful companies, asset management or Islamic funds in 
				the country. We also aim to assist the government and regulators 
				in bringing changes to legislation and regulations to ensure 
				that Islamic finance products and services are not treated less 
				favourably by the financial system.&#8194;</td>
