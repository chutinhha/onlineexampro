						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Global competitiveness of GCC economies</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>The Gulf Cooperation Council (GCC) countries are becoming 
				slowly but steadily acknowledged as global leaders with regards 
				to economic competitiveness<br>
				<br>
				By Dr Jasim Husain Ali<br>
				<br>
				</b>The recently-released 2012-13 Global Competitiveness Report 
				shows mixed results but generally improved competitiveness of 
				Gulf Cooperation Council (GCC) economies. Of the six-member 
				grouping, three countries experienced material improvement in 
				their ranking especially Qatar and the UAE. Still, Oman’s 
				ranking remained standstill whilst that of Kuwait suffered a 
				setback.<br>
				<br>
				The World Economic Forum publishes the annual report which ranks 
				reviewed economies on the basis of their performance on the 
				Global Competitiveness Index (GCI). To its credit, GCI is noted 
				for undertaking a comprehensive look into reviewed economies by 
				relying on a set of variables.<br>
				<br>
				The index ranks economies on the basis of their achievement on 
				three broad categories, namely basic requirements, efficiency 
				enhancers and innovation and sophistication factors. The basic 
				requirements category is subdivided into institutions, 
				infrastructure, macroeconomic stability, health and primary 
				education. The efficiency enhancers category comprises of higher 
				education and training, goods and market efficiency, labour 
				market efficiency, financial market sophistication, 
				technological readiness and market size. The innovation and 
				sophistication factors category is made up of business 
				sophistication and innovation.<br>
				These three categories are brought together in the formation of 
				GCI, which comprises a maximum seven points. The index is 
				developed by using publicly available data and WEF’s own opinion 
				surveys. Hundreds of business leaders are polled for the purpose 
				of gathering data for the report.<br>
				<br>
				<b>Qatar on top<br>
				</b>The latest report provides ranking of 144 economies, two 
				above the earlier one, suggesting growing appreciation on the 
				study. The report ranks Qatar as the 11th most competitive 
				economy in the world, the best within GCC, Arab and Middle East 
				and North Africa region. The achievement partly reflects 
				budgetary surpluses, in turn allowing for absence of debt 
				threat, hence macroeconomic stability.<br>
				<br>
				As a proof, the budget for fiscal year 2012-13 starting in April 
				projects a surplus of $7.7bn. Still, the report highlights other 
				points of strengths for Qatar, not least boasting per capita 
				income of $98,329, certainly an exceptional figure. Saudi 
				Arabia’s ranking fell by a single notch partly reflecting 
				inefficiencies in the labour market. The Saudi job market is 
				characterised by limited participation of Saudi women together 
				with increasing pressures on firms to offer certain desired jobs 
				to Saudi males.<br>
				<br>
				Nevertheless, the size of the economy works to Saudi Arabia’s 
				benefit, as the kingdom boasts the largest gross domestic 
				product (GDP) in the GCC and Arab world. This partly explains 
				the reason behind Saudi Arabia securing ranking No. 18 
				worldwide.<br>
				<br>
				Conversely, labour market efficiency is a point of strength for 
				the UAE, as the country provides employment opportunities for 
				people of more than 100 countries from all continents. Also, the 
				UAE is a regional leader in such areas as infrastructure like 
				the Dubai Metro. Still, continuous development and upgrading of 
				airports throughout the country together with being home to 
				global airlines such as Emirates and Etihad add to the country’s 
				economic competitiveness.<br>
				<br>
				Sadly, Kuwait saw its ranking dropped by three notches to No. 37 
				globally, thereby replacing Bahrain as the least competitive 
				economy amongst GCC economies. It is not wrong to partly fix the 
				blame on infighting between the executive and legislative 
				branches over priorities. The global ranking of GCC states could 
				be improved through integration of regional economies. 
				Comprehensive and speedy implementation of the Gulf Central 
				Market (GCM) scheme should serve the cause of all member 
				countries. Commenced in January 2008, the GCM calls for free 
				flow of factors of production amongst member states.</td>
