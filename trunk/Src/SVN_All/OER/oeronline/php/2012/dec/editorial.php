				second term gets off to a flying start with strong economic 
				growth. The tell tale signs are already there as the fears of a 
				double-dip recession have been put to rest. The fact that the 
				GDP is projected to stay at a healthy two percent plus or minus 
				in the coming year are pointers to slow but steady growth<br>
				Fiscal prudence in Eurozone :<br>
				<br>
				The embattled countries of Europe – Greece, Ireland, Portugal, 
				Spain, Cyprus and Italy can reign in their deficits and work 
				towards better fiscal prudence. Moreover, European policy makers 
				can find a meeting ground on ensuring greater economic, 
				financial, and fiscal convergence among Eurozone members China’s 
				hard landing proves to be a myth: The jury is still out on 
				whether an economic growth rate of over 7.5 per cent, after 
				decades of double digit growth qualifies as a hard landing or a 
				more realistic appraisal of growth prospects of a large economy. 
				Whichever view you may be inclined to support, one hopes that 
				China can continue growing at a reasonable pace for the sake of 
				other commodity based economies whose fortunes are intertwined 
				with China.<br>
&nbsp;<br>
				Samsung versus Apple battles are fought in the marketplace: The 
				two tech giants can settle scores in the marketplace with 
				cutting edge products rather than legal battles in courts over 
				patent infringement and technology replication.<br>
				<br>
				Jobs, jobs, jobs: From the US to Europe to the Middle East, the 
				spectre of unemployment remains the biggest social, political 
				and economic problem faced by governments and countries alike. 
				Long term unemployment affects people both economically and 
				psychologically and for the sake of the millions who are out 
				there looking for jobs, one hopes that the year 2013 brings them 
				hope, sustenance and a job.<br>
