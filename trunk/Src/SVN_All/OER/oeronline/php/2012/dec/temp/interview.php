						<span class="style27"><strong>A NEW LEAGUE</strong></span><p class="style15">
				<span class="style14"><strong>A special event was held in Muscat 
				recently to mark the Middle East and Africa preview of the 
				all-new Kia Quoris luxury flagship sedan. A report</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-oct-12-1.jpg" style="border: 1px solid #808080; " width="400" height="268"></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				The top management team from Kia Motor Corporation, Seoul, 
				consisting of Jae Yong Chung, director - Middle East and 
				Africa/Asia Pacific operations, Peter Schreyer, Kia’s renowned 
				chief design officer and Soon-Nam Lee, director - overseas 
				marketing group were in Muscat for this exclusive launch event. 
				They took time out from their busy schedule to speak to OER. 
				Excerpts:<br>
				<br>
				<b>Kia is entering a new segment with Quoris. What kind of a 
				design challenge did a car like this present? </b><br>
				Schreyer: When you do a big luxury sedan, it is a big challenge 
				for a designer as one needs to put in a lot of efforts. Having 
				worked for a German company for a long time, I have the 
				advantage of having grown up in that kind of a car culture. This 
				enables me and my team to make cars that have the best in class 
				designs and are on the same level as European manufacturers. <br>
				<br>
				<b>Are there a certain design elements in the Quoris that you 
				would like to highlight? <br>
				</b>Schreyer: For me it is not about the details but the overall 
				stance of the car, when you see it on the street and the way it 
				sits. The car exhibits dignity and sportiness and it reflects 
				the kind of car that it is. In all cars that I design, I try to 
				blend the exterior and interior design. The interiors of the 
				Quoris is very good and spacy. When you sit on the back seat, it 
				is luxurious and nice. It is not a typical chauffer driven car 
				but a driver’s car and that’s what I like about it.<br>
				<br>
				<b>The Quoris must have presented the challenge of maintaining 
				the unity of the car with other Kia brands, while 
				differentiating it at the same time. Have you managed to achieve
				<br>
				this balance? </b><br>
				Schreyer: Yes we have managed to do this successfully. It is 
				important that the car stands out from the rest of the product 
				range, to show its positioning as a premium luxury sedan, but it 
				also needs to fit into the Kia family. It is the boss of the 
				family but it is also a part of the family and that has been 
				achieved. <br>
				<br>
				<b>You have often remarked that there needs to be a balance 
				between what a designer would like to do creatively and what 
				should be done realistically in line with regulatory and safety 
				standards. Does the pressure of being real go against your 
				design aspirations?</b><br>
				Schreyer: This is our daily work. A car is not just about design 
				alone or engineering, it’s more about how design and engineering 
				strike the best possible compromise. Creating a car is always 
				about making a lot of compromises for the best possible product. 
				One has to find solutions in various details, because at the end 
				of the day all of us want to make a great car.<br>
				<br>
				<b>Kia Motors has been doing exceptionally well in various 
				markets. Given this scenario why was the need felt for a luxury 
				sedan? <br>
				</b>Chung: Our 2016 vision for Kia is to become a tier 1 brand 
				and to achieve this we needed a strategic model. If you look at 
				our range we have the Kia Soul, and the Quoris in the premium 
				luxury segment. Our domestic market needed an upmarket premium 
				car and our first objective was to satisfy this demand. 
				Secondly, in the US market we already have the Optima and Cerato 
				and to attract customers we needed to upgrade our offering. 
				Keeping this in mind we introduced the Cadenza first and the 
				next step is the Quoris. That’s the reason why we developed 
				these cars. <br>
				<br>
				<b>Customers of a premium luxury sedan are usually driven by a 
				brand name. Are you concerned that there may be few takers for 
				the Kia Quoris at a higher price point? </b><br>
				Schreyer: The only way to mitigate this perception is by 
				convincing customers about the product. I remember having the 
				same discussion at Audi, 30 years back. Audi had no value at 
				that time and it was not seen as a premium brand at all. So you 
				need to take that first step with a striking new product. You 
				cannot change the badge or brand, but you can come up with a 
				state-of-the-art product and perceptions will start to change.
				<br>
				<br>
				<b>Companies like Toyota and Nissan have created luxury brands 
				like Lexus and Infinity while entering the premium luxury 
				segment. Why was such a strategy not considered by Kia?</b><br>
				Lee: Looking at the investment and benefits that would accrue 
				from such a strategy we decided against it and planned to keep 
				our current brand name.<br>
				<br>
				<b>Where are you planning to manufacture the Quoris and what 
				kind of numbers do you expect to sell in the first year? </b>
				<br>
				Chung: The Quoris will be manufactured in Korea and we expect to 
				export 3,000 Quoris cars in the first year. This figure excludes 
				the US market as we will start production for the US at the end 
				of 2013. In the Middle East we are looking at selling 1,600 cars 
				in a year or so. <br>
				<br>
				<b>How is Kia Motors performance been in 2012 and have you been 
				impacted by the slowdown in Europe? </b><br>
				Lee: We have seen a 12 per cent growth till July 2012 compared 
				to the seven months of last year and are on course to better our 
				target of 9.5 per cent growth in 2012. Though Western Europe is 
				going through a slowdown, Kia has seen a 23 per cent growth in 
				that market compared to 2011. Similarly, we have seen double 
				digit growth in Middle East and Africa. In China we have grown 
				by over 15 per cent. There has been a huge perception change 
				towards Kia.<br>
				<br>
				<b>How has Kia’s performance been in emerging markets?</b><br>
				Lee: We have been doing very well in South East Asian countries 
				like Indonesia, where we have doubled our volumes, and in 
				Malaysia. Our biggest concern in Asia is that we are not in a 
				position to give our distributors the number of cars that they 
				demand from us.<br>
				<br>
				<b>Your new design for Kia with the Tiger nose grill has been a 
				great success. How are you going to improve upon it? </b><br>
				Schreyer: We are a very young brand and we are at the beginning 
				of our journey into the future. This is the first time that we 
				have been able to create a family feel for the entire product 
				range. You can see a homogenous, coherent product range from the 
				Picanto upto the Quoris. We have to continue to be proud and 
				confident about what we have achieved. We are still in a 
				learning phase in a positive manner. We have introduced our 
				front grill and our face, but this is something that we have had 
				only for 4-5 years compared to others who have had a design 
				identity for 100 years. Of course we will surprise our customers 
				but we are building a brand and it takes a long time. A brand 
				needs to give customers a feel of reliability and assurance that 
				the next car is better than their previous car. <br>
				<br>
				<b>What was the inspiration for the signature Tiger nose grills 
				that Kia cars flaunt? </b><br>
				Schreyer: Though we called it the Tiger nose later on, the tiger 
				was not the inspiration, we were working on a show car in 2007. 
				I was new and was desperately thinking about making a grill that 
				would give a good impression, hold value for the future and be 
				something that could be used for generations of cars. The grill 
				is a very strong identifier, even when you see it from a 
				distance or even without the badge and it just came up during 
				work. It was only later that I used the Tiger metaphor to give 
				our designers a sense of how a three dimensional front should 
				be.&#8194;</td>
