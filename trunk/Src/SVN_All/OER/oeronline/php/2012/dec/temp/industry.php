						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					Successful diversification</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Anvwar Al 
					Balushi, Group CEO, Majan Consolidated says that the success 
					of his company comes from self-belief and determination to 
					succeed, come what may. 
					An OER report</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/industry-nov-12-1.jpg" style="border-style: solid; border-color: #808080; "></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				Of late, Majan Consolidated is in an upbeat mood thanks to the 
				intervention of the Minister of Commerce and Industry to solve 
				the additional gas supply to its subsidiary Hadid Majan, a steel 
				rolling mill located in Rusayl Industrial Estate. This is a 
				result of extensive efforts by Anvwar Al Balushi, group CEO, who 
				left no stone unturned to negotiate extra gas for the expansion 
				plans of Hadid Majan, which in a way is the flagship of the 
				group.<br>
				<br>
				“HE Ali bin Masoud Al Sunaidy, the Minister of Commerce and 
				Industry, had delegated a special team to solve this issue at 
				the earliest and we are working hand in hand with the Ministry 
				to resolve the gas crunch Hadid Majan is facing,” says Balushi. 
				Established in 2003, Hadid Majan expanded its operations by 
				launching its automatic steel rolling plant last year. Now, this 
				automatic plant, established at a cost of RO5mn, faced an 
				uncertain future due to gas shortage. “But after the minster’s 
				intervention, I feel that the gas crisis will be resolved soon. 
				We may get at least half of what I have asked for so that I can 
				increase the production by 150 per cent. This will cushion my 
				present operational problems to an extent,” Balushi says.<br>
				<br>
				“Hadid Majan started with a production capacity of 5,500 tonnes 
				per month consuming 10,000 cubic meters of gas per day. Over the 
				years it enhanced production and expansion and after 2008, we 
				went into expansion. The expansion was for an additional 12,500 
				tons. Based on the existing success, the plant is ready but we 
				were waiting for gas. But now things are going positive. I can 
				say that based on my request to the government, we can expect 
				the allocation of gas, in two or three phases. Once the full 
				allocation is done, our production will be three times the 
				existing amount and the turnover will shoot up,” Balushi says.<br>
				<br>
				According to him, Hadid Majan’s expansion is an indicator of the 
				direction in which Majan Consolidated is progressing – on the 
				growth path. “In 2008, when recession hit all around, we as a 
				group were expanding full-fledged. We invested in Hadid Majan’s 
				expansion, even though the demand for commodities was going 
				down. We made plans to recruit staff and train them and further 
				we continued to look for other investment opportunities,” 
				Balushi points out while talking about the group’s performance 
				in the last few years.<br>
				<br>
				“Our new plan is to raise our Omanisation target upto 46 per 
				cent within the next three to six months and also to provide 
				inhouse training to the Omani staffs to make them more efficient 
				and productive. In one and a half year’s time, we want to make 
				it 58 per cent. By then our total staff strength will go up to 
				500 people. This will be a major contribution to the economy of 
				the country,” Balushi discloses.<br>
				<br>
				<b>Diversification drive<br>
				</b>The company has a simple policy for growth – diversify and 
				expand. Under Balushi’s leadership, the group has ventured into 
				education, healthcare, travel &amp; tourism, real estate, 
				agriculture and the entertainment industry. Presently, the group 
				is involved in two antibiotic manufacturing projects – one in 
				Buraimi and the other in Sohar, for animals and humans 
				respectively – at a total cost of around RO7mn. The Buraimi 
				plant is the first of its kind for the Sultanate. The veterinary 
				antibiotic plant, known as Muscat Pharma Manufacturing Co, has 
				begun its trial production in Buraimi. “The Sohar antibiotic 
				manufacturing plant, known as Drug Manufacturing Company, is 
				export oriented. Presently this plant is in the construction 
				stages and trial production will begin after a year,” informs 
				Balushi.<br>
				<br>
				Apart from the drug manufacturing companies, the group has plans 
				to focus on the tourism industry with Jahloot resort, a 
				multi-phase, development project plan.<br>
				<br>
				The company has also received a verbal approval and waiting for 
				an official letter from the concern authorities to expand the 
				existing Riyam Park covering an area of 25,000 sq mtrs. It will 
				feature several attractions catering to the entire family. These 
				include the ‘Souq Square’ the eye of Riyam, the Giant rides, the 
				skate board, the mountain climbing, ‘Water Theme Park’ the water 
				tower, ‘Riyam Square’ the ship and ‘Children Fun Park’ the 
				wheel.<br>
				<br>
				The parent company of Majan Consolidated Companies is Wadi 
				Muscat Trading and Contracting Enterprise incorporated in 1977. 
				The company had been engaged in construction and related 
				maintenance activities. Under its banner, the company has been 
				operating Riyam Amusement Parks since 1995, which is one of the 
				popular holiday destinations for the people residing in and 
				around Muscat.<br>
				<br>
				The group had also commissioned Hadid Majan, a steel 
				manufacturing unit in 2003 to produce and supply high yield 
				strength deformed steel bars under the name ‘Majan Steel Bars’ 
				to infrastructure projects across Sultanate.<br>
				<br>
				In 2006, the group took over a higher secondary school known as 
				National Private School that has a history of over 25 solid 
				years in Oman and today NPS is one of the best educational 
				institutions in Oman. The school also has a primary school 
				branch in the Mawaleh area.<br>
				<br>
				Well deserved recognition <br>
				Balushi’s immense contributions to Majan Consolidated have been 
				recognised regionally and internationally. He was the chief 
				guest at the convocation ceremony of India’s Shri Balaji Society 
				(it owns several international institutes and colleges) in 
				January this year becoming the first Omani corporate personality 
				to be invited for such an event. He was also included on the 
				elite panel of the ‘2012 East Annual Financial Thought Leaders 
				Summit’, in Dubai in March 2012.<br>
				<br>
				Recently, Balushi was honoured the third rank in the Middle East 
				and North Africa region as a ‘Revolutionary CEO of the Year’ 
				category at the MENA CFO of the Year Awards 2012, which was 
				organised by Naseba a dubai based french company who produces, 
				promotes and hosts business summits, professional training 
				courses and business exhibitions targeting executive level 
				attendees across multiple industries. Naseba also has their 
				ground presence in Monaco, Cairo, Riyadh, Bangalore Kaula 
				lampur. Al-Balushi became the first Omani to be selected for 
				this prestigious honour. This was in recognition of Balushi’s 
				accomplishments as an executive and entrepreneur with an 
				experience in managing diverse business interests. The honour 
				recognised his vast business experience in industries that 
				include banking, steel manufacturing, education, travel and 
				tourism, amusement, real estate, healthcare, construction, 
				agriculture and mining.<br>
				<br>
				“The man behind my success is my guide and teacher, our chairman 
				HH Sayyid Barghash bin Said Al Said. Under his guidance and 
				advice I have managed to do all this. If he had not given me an 
				opportunity and a free hand I would not have been able to do 
				anything. I thank him for the trust he has reposed in me,” 
				Balushi says.<br>
				<br>
				As part of its future plans, Majan Consolidated is also planning 
				to start a media university in Muscat. Talks are underway with 
				several educational institutes to find a good academic partner. 
				The project cost of this university is expected to be around 
				RO30mn.<br>
				<br>
				“I want to stick to expansion and diversification. Because of my 
				connections, I have lots of projects on hand. If I begin to work 
				on them, it will take my whole lifetime. But I am all for growth 
				and I will keep growing. Right now, we have plans to get into 
				mining and hospitals,” Balushi says.<br>
				<br>
				In the light of the achievements made by his company, Balushi 
				says that his advice to young entrepreneurs is, don’t stop even 
				if it is dark. If it is dark, just light a candle and keep 
				going. Don’t make excuses because time is money and time is 
				value. If there is a huge stone, make a staircase and climb over 
				it.</td>
