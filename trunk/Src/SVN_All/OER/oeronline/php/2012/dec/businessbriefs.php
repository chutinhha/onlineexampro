						<p align="justify" class="style10">
				The US administration will not impose anti-dumping duties on 
				Omani manufacturers suspected of selling steel pipes in that 
				country at below-fair value. This decision follows an 
				investigation by the US Department of Commerce on a petition 
				filed by a consortium of American steel producers seeking 
				“import relief” against what they claimed as dumping by Omani 
				pipe manufacturers.<br>
				<br>
				The petition was filed jointly by Pennsylvania-based steel firms 
				US Steel Corp and Wheatland Tube Co, as well as Illinois-based 
				Allied Tube &amp; Conduit and JMC Steel Group of Chicago. They 
				claimed that dumping by exporters from Oman hurt steel plants in 
				16 US states. The US Department of Commerce’s International 
				Trade Commission decided against levying any countervailing 
				duties against Omani pipe exporters, on the ground that there 
				was little or no evidence of subsidies enjoyed by these 
				companies.<br>
				<br>
				“We warmly welcome this important decision by the US 
				International Trade Commission,” remarked Nasima Yahya Al 
				Balushi, director general of PAIPED’s Export Directorate.</td>
