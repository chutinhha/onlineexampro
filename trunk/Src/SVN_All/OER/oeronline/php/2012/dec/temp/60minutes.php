					<b><font face="Verdana" size="4" color="#C00000">
					People friendly lighting</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Ryozo Endo, 
					founder of Endo Corporation, a pioneer in the LED Lighting 
					Solution speaks to OER about LED lighting and the company’s 
					association with Al Hassan Electricals</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/60minutes-nov-12-1.jpg" style="border-style: solid; border-color: #808080; "></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				As environmental awareness continues to grow, LEDs 
				(light-emitting diodes) are considered as an innovative source 
				of lighting. In this respect, Endo Lighting Corporation, a 
				pioneer in the LED lighting solutions from Japan, is not only 
				committed to determining actual brightness and efficiency values 
				but is also dedicated to researching the essence of light and 
				creating environments with high added value.<br>
				<br>
				Endo Lighting Corp was founded in Osaka in 1967 as a 
				manufacturer of original lighting fixtures. The products and 
				services the company has provided since that time originates 
				from a dedication to create light that increases the value of 
				its surroundings alongwith technologies that are necessary to 
				ensure the optimal projection of such illumination.<br>
				<br>
				Endo Lighting Corp is Japan’s first publicly listed lighting 
				manufacturer and is currently the leading company in the 
				architectural and commercial segment of the lighting market. 
				Even though, LED lighting was popular due to its power-saving 
				qualities, the power shortage created by the devastating effects 
				of the 2011 tsunami made Japanese people to think of energy 
				saving lighting solutions more seriously. Following this, Endo 
				Lighting Corp has gained much prominence.<br>
				<br>
				According to Ryzo Endo, president and CEO and founder of the 
				company, basically LED is an energy efficient technology 
				resulting in electricity saving and environment protection. 
				Endos’ LED products are both eco-friendly and people-friendly, 
				he adds.<br>
				<br>
				Continuing further, Endo says, “environmental conservation 
				initiatives are a central focus of our operations, and we work 
				to develop technologies and provide lighting expertise that 
				delivers high value lighting environments with less energy. We 
				strive not only to help architectural and commercial facilities 
				boost business performance with dramatic lighting solutions, but 
				also enable people to coexist with the environment with energy 
				efficient lighting.”<br>
				In Oman, Endo Lighting Corporation plans to service country’s 
				high-end market with its latest LED lights, which are 1.5 times 
				brighter than standard modules and uses less energy. Says Endo, 
				“The projects where our products would find application are 
				hotels, offices, shopping malls, villas and upscale buildings. 
				Endo network is spread around 45 cities around the world and in 
				Oman we are associated with Al Hassan Electricals.”<br>
				<br>
				The agency distribution agreement with Al Hassan Electricals 
				gives Endo a strong foothold in the residential and professional 
				lighting sector and will give its lighting products a wider 
				reach in Oman, says Endo.<br>
				<br>
				Al Hassan Electricals has successfully introduced Endo and 
				already supplied and installed Endo light-fittings in few major 
				projects like Duqm Beach Hotel, Oman Arab Bank Head Office 
				building, Asian Beach Games and Al Majlis Shura building.<br>
				<br>
				On their association with Al Hassan Electricals, Endo says, 
				“being the flagship company of Al Hassan Group, Al Hassan has 
				always been in the forefront to bring the best of international 
				and national products to consumers in Oman and acts as a 
				catalyst between the manufacturers and end-customers. He added 
				that organising major events like the Endo LED seminar is part 
				of AHE’s endeavour to increase customer awareness about the 
				latest technological and design innovations in the lighting 
				sector. He stressed that a partner like Endo is particularly 
				appreciated by lighting designers and project managers as a 
				company whose technical skills, R&amp;D and innovative products 
				brings to the customer value for money.”&#8194;</td>
