				<br>
				If the 20th century was an age of impersonal wars, the 21st 
				seems like an age of personal malice. The rage thermometer is 
				way up. Families quarrel, co-workers contend, cyber bullies 
				terrorise, courts are jammed, and fanatics murder the innocent. 
				In any conflict, the first alternative is my way and the second 
				alternative is your way. The fight usually boils down to a 
				question of whose way is better. There are many methods of 
				“conflict resolution,” but most involve compromise, a low-level 
				accommodation that stops the fight without breaking through to 
				amazing new results. The book under review, The 3rd Alternative, 
				Solving Life’s Most Difficult Problems, presents a new and 
				practical, but incredibly effective and eye-opening, way to 
				reach resolution through using the ‘third alternative’. The 
				third alternative moves beyond your way or my way to a higher 
				and better way – one that allows both parties to emerge from 
				debate or even heated conflict in a far better place than either 
				had envisioned. With the third alternative, nobody has to give 
				up anything, and everyone wins.<br>
				<br>
				Written by the multimillion-copy bestselling author of The 7 
				Habits of Highly Effective People – Stephen R Covey – the book 
				introduces a breakthrough approach to conflict resolution and 
				creative problem solving. The 3rd Alternative is about more than 
				just an armistice – it’s about creating a new and improved 
				reality. A departure from the usual approaches to conflict 
				resolution, negotiation, and innovation, this book reveals a new 
				way of thinking that will be embraced not only by the many fans 
				who have flocked to Covey’s prior books, but also by anyone who 
				is seeking solutions in their professional or personal lives.<br>
				<br>
				To a world of escalating strife and contention, 3rd Alternative 
				thinkers like those Covey profiles here bring creative 
				solutions, peace, and healing. Through key examples and stories 
				from his work as a consultant, Covey demonstrates the power of 
				3rd Alternative thinking. His wide-ranging examples include: a 
				Canadian metropolitan police force that transformed a 
				crime-plagued community by abandoning their ‘them vs. us’ 
				mentality and changing the whole definition of police work; a 
				father who rescued his troubled daughter from years of despair 
				and near suicide in one surprising evening; a judge who brought 
				a quick, peaceful end to one of the biggest environmental 
				lawsuits in American history without setting foot in a 
				courtroom; the principal of a high school for the children of 
				migrant workers who raised their graduation rate from a dismal 
				30 per cent to 90 per cent and tripled their basic skill levels; 
				a handful of little-known people who are quietly finding new 
				ways to bring peace to the Middle East; and many others. These 
				various groups and individuals offer living examples of how to 
				create new and better results instead of escalating conflict, as 
