						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Obstacles: Perception vs reality</font></b><p class="style15">
				<span class="style14"><strong>NTI BizPro Awards 2012 were 
				presented to young achievers after a rigorous filtering process 
				by KPMG. Mayank Singh and Visvas Paul D Karra report</strong></span><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-july-12-6.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="500" height="333"></font><p align="justify" class="style13" dir="ltr">
				<b>Higher operating costs</b><br>
				When production levels are affected due to such work stoppages, 
				the results can be disastrous. With the current salary levels 
				and Omanisation percentages, the operating costs of businesses 
				in Oman are somewhere between the Asian and European countries. 
				Our costs are 15-16 per cent higher than Asian countries and 5 
				per cent less than European countries. Any further increase in 
				costs which is not compensated by an increase in productivity 
				can be disastrous for the industry.” <br>
				<br>
				Says Alok Bhargava, CEO, Voltamp Energy, “We are competing 
				against MNCs and Asian countries and if there are further cost 
				increases we will not be able to compete. In fact, any further 
				increase in manpower costs, which is not compensated through 
				government policies, can be disastrous for the manufacturing 
				industry and FDI will be impacted. But I am confident that 
				things will settle down quickly.”<br>
				<br>
				Continuing further, Bhargava says, “We increased salaries last 
				year, but now workers are asking for RO350. Our minimum salary 
				is RO255, we have an insurance cover for all our workers and 
				Voltamp has implemented a five-day week. We have been under 
				pressure from MoM sometimes to hire more Omanis. For a high tech 
				factory like ours at Sohar, it takes at least three years before 
				an untrained person can really add to productivity. Though we 
				have taken 36 per cent Omanis and want to increase it to 40 per 
				cent, the company will need to invest in them for three years, 
				before they reach acceptable levels of productivity. Therefore, 
				initially our costs will be high and need to be compensated in 
				some way by the government.”<br>
&nbsp;<p align="center" class="style13" dir="ltr">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-july-12-7.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="500" height="333"></font><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Manpower issues<br>
				</b>Ramesh Mani, CEO, Majan Glass Company, says that getting 
				Omanis has become difficult since February 2011 and he wants the 
				MOM to be a bit more flexible. When the government announced 
				50,000 jobs a lot of people went away to the army and the 
				government. The day the government announced that it will 
				provide a RO150 unemployment allowance, a number of workers from 
				Majan Glass wanted to resign and avail of those benefits. So it 
				was difficult to fill up the 35 per cent Omanisation level and 
				without fulfilling this mandatory criteria, the company was not 
				being given labour clearances, so it was a dual edged sword.<br>
				<br>
				Says Mani, “Being a continuous process industry, we could not 
				shut down our plant as the plant produces two million bottles a 
				day. We should have been treated leniently but it did not 
				happen. The choice was either to run the plant or to shut it 
				down. We interviewed 120 Omanis and offered jobs to them, but 
				out of them only five or six stayed back. We went in for 
				contracted labourers to run the plant, but we had people from 
				the MOM coming and conducting raids in the industrial estate and 
				finding faults. For example, the MoM officials came and fined us 
				for people who were multitasking. We have bottles being made and 
				if an Omani worker does not turn up, we need someone to pack the 
				bottles and if an expat worker with a different designation 
				substitutes for an Omani worker, we get fined.”<br>
				<br>
				Two of Majan Glass workers were fined RO1000 each and the MoM 
				officials took away their labour cards for 15 days. This left a 
				bad taste in the mouth. Then the chairman of Majan Glass took up 
				the issue strongly, and Mani too met the DG of the MoM and 
				finally the company start getting labour clearances. It was a 
				situation that could have been handled more intelligently.<br>
				<br>
				<b>Excerising rights unfairly <br>
				</b>Ensuring labour rights is pre requisite for any country to 
				ensure an equitable growth. However the pace of reforms in Oman 
				have been too quick to sink in. Collective bargaining is a 
				standard practice in any industrialised country but workers in 
				many factories in Oman are not mature enough to use it in legal 
				manner. They have become emboldened and they think they can 
				demand anything and would get it, says Sundeep Mudgal, general 
				manager, Taghleef Industries Oman.<br>
				<br>
				Actually, it is unfair to expect that workers would use their 
				new found rights in a mature manner so quickly. It is the 
				government and confederation of industries who need to show a 
				more balanced approach in these matters. They need to caution 
				workers to respect law and find a way to work with company 
				managements. It has been seen many times that they have given 
				one-sided support to workers’ demands. “Our company was on the 
				brink of a strike earlier in this year, thankfully we resolved 
				it by a long negotiation with workers’ union,” adds Mudgal. <br>
				<br>
				Promoting an entrepreneurial culture is about making people – 
				especially young people – aware of the potential of setting up 
				and running a business as an alternative to employment in 
				government and the private sector. Even though there are 
				challenges, overall, there are a number of initiatives to 
				promote entrepreneurial culture and, together with broader 
				government initiatives to foster private sector development, 
				there should be no shortage of opportunities for budding 
				entrepreneurs. It is upto the younger generation to rise up to 
				the challenge and contribute to the country.<br>
				<br>
				<br>
				<br>
				BOX – 1<br>
				<br>
				<b>SNAPSHOTS OF OBSTACLES<br>
				</b>Marketing is the biggest challenge many small businesses 
				face<br>
				Lack of support from family and friends to buy new products and 
				spread the news<br>
				Big companies dominating market and smaller businesses not 
				getting share of projects<br>
				Administrative issues like bureaucratic delays in registrations 
				etc.<br>
				Oman is small market and poses tough challenge to sell products 
				and services<br>
				People are more of savers and spend only on necessities and buy 
				few luxury products<br>
				Labour regulations and lack of clarity in Omanisation policy<br>
				Multi-tasking not allowed in manufacturing units<br>
				Lack of proper awareness and education about entrepreneurship<br>
				Factory workers not mature enough to use the power of collective 
				bargaining in legal manner<br>
				With the current salary levels and Omanisation percentages, 
				operating costs are going up<br>
				Work stoppages in various companies could have ramifications on 
				new investments<br>
				<br>
				<br>
				<br>
				<br>
				BOX – 2<br>
				<br>
				<b>ILO SURVEY ON ENABLING ENVIRONMENT FOR SUSTAINABLE 
				ENTERPRISES ASSESSMENT<br>
				</b><br>
				ILO survey on enabling environment for sustainable enterprises 
				assessment<br>
				Peace and political stability: 15 per cent of survey respondents 
				said that political instability/uncertainty was affecting their 
				business a lot while 48 per cent said the instability was 
				affecting their business to some extent<br>
				Good governance: Most firms believe that corruption and bribery 
				hinders the performance of Omani firms while female-owned or 
				female-run firms felt corruption and bribery had a very negative 
				impact on firms’ performance.<br>
				Social dialogue: The current situation in terms of workplace 
				relations and the related legislative frameworks in Oman is 
				fluid. Recent legislative changes have enabled the freedom to 
				establish labour unions in all firms and the right to bargain 
				collectively, but it appears that these legislative changes have 
				led to some confusion<br>
				Enabling legal and regulatory environment: 70 per cent of firms 
				believe that Oman’s regulatory environment makes it difficult 
				for small businesses to compete with large businesses<br>
				Nearly 40 per cent of firms felt that business required dealings 
				with the Government can be too bureaucratic and could be 
				improved<br>
				Fair competition: Competition policy remains a concern for the 
				business community. Nearly 1 in 2 firms describe competition 
				policy and legislation in Oman as not efficient or only 
				sometimes efficient in preventing unfair competition. Firms in 
				the manufacturing sector in particular are concerned about fair 
				competition<br>
				Entrepreneurship culture: The survey results suggest that there 
				is a positive view of entrepreneurship from young people (41 per 
				cent sharing this view). However, a large sample of enterprises 
				surveyed (43 per cent) believe that only a small minority of 
				young people have the right skills to successfully run a 
				business<br>
				NOTE: This primary data collection consisted of 150 face to face 
				interviews with a cross section of firms, of varying sizes and 
				sectoral activities from across the country. The survey was 
				conducted by Arabian Research Bureau, under the aegis of the 
				ILO.</td>
