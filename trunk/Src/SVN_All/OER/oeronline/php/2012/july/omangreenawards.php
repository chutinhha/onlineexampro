						Creating a green umbrella<br>
						</font><font face="Verdana" size="2">
                		<br>
						The third edition of Oman Green Awards honoured 
						outstanding environmental vision, endeavours and 
						achievements of corporates and individuals</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-1.jpg" width="400" height="267"><p align="center" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">Oman Green Awards (OGA) 
						2012 has once again proved that creating a green economy 
						is the need of the hour as environmental issues and 
						economic development increasingly intersect and 
						complement each other. Nineteen finalists, including two 
						joint winners, won awards in nine different categories 
						of OGA at a function held at Al Bustan Palace Ritz 
						Carlton Hotel, under the auspices of HE Mohammed Said Al 
						Toobi, Minister of Environment and Climate Affairs.<br>
						<br>
						In a specially created perfect green ambience at the 
						Majan Ballroom, a discerning audience of more than 200 
						guests, comprising top representatives of Oman’s 
						corporate houses, schools and other non-governmental 
						institutions, applauded in acknowledgement of the fact 
						that OGA is a ideal green platform to stir up people’s 
						interest and awaken positive activism for environmental 
						goals and greater action in the long term, both at 
						community and decision-making levels.<br>
						<br>
						Coinciding with the World Environment Day on June 5, OGA 
						has been conceptualised by OER and United Media Services 
						(UMS) and is being held in association with the Ministry 
						of Environment and Climate Affairs, Muscat Municipality 
						and the Ministry of Health.<br>
						<br>
						In her welcome address, Alpana Roy, executive vice 
						president, UMS said that Oman Green Awards, over the 
						past three years, had recognised exemplary efforts made 
						by companies and individuals in promoting environment 
						preservation and sustainable development in the 
						Sultanate. “OGA has raised awareness about going green 
						and the necessity of saving the natural beauty of this 
						pristine country,” she added.<br>
						<br>
						The event received massive support from corporate houses 
						and individuals who have made green mission a priority. 
						Oman Oil Marketing Company, Octal and Muriya Tourism 
						joined as Green Oman partners to keep the green torch 
						burning bright in the country. The media partners of the 
						event were Times of Oman and Al Shabiba while the 
						support partners were Sadolin, Green Cover, Infoline and 
						Oman Printers and Stationers.<br>
						<br>
						As part of the function, young Omani scientist Mustafa 
						Barami gave a presentation of his path-breaking 
						invention which can recycle a kilo of palm leaves into 
						80 sheets of A4-sized paper. His invention showcases how 
						innovation and environment conservation can go 
						hand-in-hand, by making use of the cellulose found in 
						the palm leaves which would otherwise be burned as waste 
						by date-palm farmers.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
