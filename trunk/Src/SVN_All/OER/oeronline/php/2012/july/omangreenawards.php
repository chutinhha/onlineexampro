						<b>Towards cultivating green consciousness</b><br>
						<br>
						Caledonian College of Engineering won the Green Campaign 
						Award for various outstanding initiatives in 2012 to 
						generate green awareness<br>
						<br>
						As part of its Green Campaign 2012, Caledonian College 
						of Engineering has undertaken various initiatives for 
						environmental protection and awareness such as 
						organising International Workshop on Waste to Energy 
						(IWWE) at the campus, initiating several research 
						projects and publications by staff and students on 
						renewable energy applications, tree plantation within 
						campus, and implementing PV based solar power generation 
						within campus.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-5.jpg" width="264" height="175"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						The IWWE, hosted by the college at its Al Hail campus on 
						March 12 and 13, 2012, explored issues concerning solid 
						waste management, waste treatment, waste disposal and 
						the future of anaerobic digestion. The workshop was 
						facilitated by seven keynote speakers of international 
						standing, who delivered keynote speeches to an audience 
						of guests, local experts, engineering faculty from a 
						number of key institutions in Oman and CCE’s own staff 
						and students. Furthermore, 13 technical papers were 
						presented over the course of the two days, allowing for 
						discussion of the research findings in the area related 
						to sustainable solid waste management, waste management 
						policies and strategies, advanced waste treatment 
						technologies and waste disposal, together with advances 
						in waste-to-energy initiatives. The workshop was 
						attended by more than 150 delegates from industries, 
						government organisation and educational institutions. In 
						addition, the college is the leading sponsor of Zeena Al 
						Towayya, the first Omani woman to set foot on Antarctica 
						as part of the International Antarctic Expedition (IAE) 
						2041. A Caledonian Honours graduate in Computer 
						Engineering in 2008, Al Towayya was selected to be part 
						of the expedition team.<br>
						<br>
						<b>Think and act green</b><br>
						<br>
						The special commendation in Green Campaign Award went to 
						Radisson Blue Hotel Muscat which embarked on an 
						ambitious programme seeking to reduce energy consumption 
						by 25 per cent over the next five years<br>
						<br>
						Think Planet’ launched by Radisson Blue Hotel in January 
						2012 is an ambitious energy saving programme aimed at 
						reducing energy consumption by 25 per cent over the next 
						five years. The hotel reads consumption metre on a daily 
						basis to record and monitor the consumption and prepares 
						monthly reports; and sets a benchmark by a third party 
						to measure the consumption. The hotel has installed 
						energy saving bulbs, temperature control systems and 
						magnetic switches in all its rooms and corridors and as 
						part of its water saving programmes, the re-use of 
						towels and linen is suggested in every room to every 
						guest. All televisions in the rooms are equipped with 
						infra-red sensors which would automatically switch off 
						the TV if the room is not in use and all its windows are 
						double glazed to reduce the loss of temperature. All the 
						flush tanks in bathrooms are eco-friendly, using only 7 
						litres of water per flush.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-3.jpg" width="400" height="238"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						‘Think Planet’ is more than just an energy cost-cutting 
						exercise. It is about contributing to a sustainable 
						society. As part of its plan to cut energy consumption 
						five per cent in 2012, the hotel has prepared an Energy 
						Action Plan pack which serves as a toolbox to achieve 
						the 2012 target and even go beyond. The Energy Action 
						Plan is the guideline for the general manager, chief 
						engineer and heads of department to achieve the 2012 
						target of five per cent energy saving. The document 
						describes: Think Planet habits- a wide range of good 
						operational and maintenance practices; Think Planet 
						tools-energy saving tools with a return on investment 
						(ROI) of maximum two years. Considering training the 
						staff on smart Think Planet habits a key part of 
						reducing energy consumption in the hotel, he hotel 
						created three training movies under the motto ‘a picture 
						says more than a 1000 words’.<br>
						<br>
						<b>Conserving soil<br>
						</b><br>
						A group of graduates from Sultan Qaboos University won 
						the Green Champion Award for their documentary movie 
						portraying the importance of soil conservation<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-6.jpg" width="385" height="325"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						The soil that we use is integral to our livelihood. More 
						and more people are beginning to use eco-friendly 
						products and live an environmentally conscious life, 
						however only a few realise that the protection and 
						conservation of soil is equally important. While many 
						people invest valuable time and effort in keeping the 
						air and water clean, widespread awareness of soil 
						conservation is still lacking. An innovative stride 
						towards bridging this gap was taken by a group of fresh 
						graduates from Sultan Qaboos University (SQU) who did a 
						documentary movie portraying the importance of soil 
						conservation. Jointly directed by Abdullah Al-Rawahi, 
						Mousa Al-Subeihi, Suleiman Al-Sumri and Marwa Al-Siyabi, 
						the documentary seeks to educate society and students to 
						conserve soil and explore how to test the quality of 
						soil in fields and laboratories. Soil conservation 
						comprises all the procedures and methods that involve 
						protecting the soil from natural as well as manmade 
						destructions. There are many natural ways in which soil 
						could get eroded or blown away. In the same way, soil 
						could also get contaminated or chemically altered owing 
						to acidification or salinisation, or destruction from 
						machinery. Soil conservation is as important as any 
						other form of conservation that attempts to safeguard 
						the environment and improve the quality of life of all 
						living beings. The documentary was selected as an 
						outstanding project by Soils, Water and Agriculture 
						Engineering Department at SQU in 2011 and bagged the 
						students creativity award in the University. These 
						youngsters are looking to show the documentary on 
						National Geographic, BBC, Al Jazeera and other channels.<br>
						<br>
						<b>Sustainable architecture<br>
						</b><br>
						Architectural Wall Systems which introduced sustainable 
						and environmentally friendly architectural systems 
						secured the special commendation in Green Champion Award<br>
						<br>
						Architectural Wall Systems has pioneered various 
						architectural, engineering, product and educational 
						green initiatives in the Sultanate. During the last 
						three years, the company has tried to ensure an element 
						of green in every one of its projects either through 
						materials specified, systems adopted or technology used. 
						It could also create an awareness of green products that 
						can be utilisd in various construction projects in the 
						Sultanate. Architectural Wall Systems has customised its 
						services and products to suit the climatic condition and 
						market demand of the Sultanate and rationalised the 
						information available to convey an adaptable message to 
						public for promoting green concept in Oman.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-7.jpg" width="275" height="207"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						It could also simplify the green process to suit 
						clients’ budget and spatial requirements. The company 
						received positive feedback and enquiries about the 
						process involved. This feedback has strengthened its 
						conviction that there is some impact on the society and 
						a small niche has been established for green 
						initiatives. The positive feedback from the clients to 
						its architectural &amp; engineering green initiatives, 
						consumer response to its green product initiatives and 
						public response to its green education and green 
						campaigning initiatives testify to the success and 
						acceptability of the system. Some of the projects where 
						the system was successfully applied include Caledonian 
						College of Engineering‘s Campus III at Airport Heights 
						in Bausher, Campus I &amp; II at Al Hail, Moosa Abdul Rahman 
						Hassan Company’s office and service centre in Ghubra 
						etc.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-4.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						<b>Sustainable curriculum<br>
						</b><br>
						British School Muscat, the winner of Green Education 
						Award, has integrated sustainability in the curriculum 
						of all students from Nursery to Year 13<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-8.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						British School Muscat (BSM) has conducted a primary 
						geography conference on sustainability to raise the 
						importance of sustainability within primary schools and 
						to offer case studies, ideas and practical activities, 
						which can be used in schools. Held at the school on 
						February 23 and 24, 2012, the conference was attended by 
						40 delegates, which included teachers from international 
						schools across Oman, primary and senior school students 
						from BSM, representatives from various organisations. In 
						addition, the school has been working with Environment 
						Society of Oman (ESO) to bring Foundation for 
						Environmental Education (FFE) accreditation to Oman, in 
						order that schools can apply to become Green Flag 
						Schools. A network of teachers, with an interest in 
						sustainability, has been set up by the school with the 
						aim to cascade information and develop projects to 
						include as many schools and students as possible in the 
						future. The school has active environmental teams which 
						run from Year 1 to Year 6: Green Team, Recycling Team 
						and Eco Warriors. The Primary Green Team comprises two 
						children from each class from Year 1 to Year 6. Eco – 
						Warriors is an after school activity. A team of children 
						from Years 3 to 6 meet once a week. Their projects have 
						included planting vegetables; visiting the Oman Botanic 
						Gardens to learn about native species and following this 
						up by planning 15 desert roses and placing them around 
						the school; creating a pot pond and bird bath with the 
						help of Anne Love – author of Gardening in Oman and the 
						UAE; looking at bird life in the school environment etc.<br>
						<br>
						<b>Catching them young<br>
						</b><br>
						A slew of measures to raise the green consciousness of 
						the students enabled Indian School Muscat’s Eco Club to 
						walk away with special commendation in Green Education 
						Award<br>
						<br>
						Young students are the pillars for the future and 
						therefore teaching them about sustainability and 
						conservation and making them actively involved in green 
						initiatives will be quite effective in the long run. 
						Indian School Muscat’s Eco Club strives to inculcate 
						strong environmental values among the students through 
						various green initiatives in and around the campus. The 
						school has incorporated environmental education into the 
						curriculum and provides a vast array of knowledge about 
						conservation and energy consumption to the students 
						during the health/education periods. By placing the 
						responsibility on the students, the school enables them 
						to come out with innovative, original ideas, and 
						eco-friendly methods.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-9.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						The club actively engages the students in a number of 
						awareness generation activities on environment, energy, 
						climate change, education and communication. It 
						encourages the students to transform thoughts into 
						action by participating in environmental friendly 
						activities in the school, and organise competitions to 
						extract creative ideas from young minds. Students 
						actively take part in making their immediate 
						surroundings clean, green and healthy and they also 
						motivate their friends and family to do the same. Club 
						members have secured the support of the administration 
						and implemented eco-friendly ideas not only in the 
						classrooms but also in the rooms of the principal, vice 
						principal and other faculties by maintaining indoor 
						plants and displaying eco-friendly tips on the display 
						boards .<br>
						<br>
						<b>Offsetting carbon footprint<br>
						</b><br>
						OCTAL, the winner of Green Footprint Award, has 
						demonstrated its technical leadership and steadfast 
						commitment to reducing environmental impact and carbon 
						footprint since its inception<br>
						<br>
						OCTAL has successfully eliminated five energy-intensive 
						stages of the conventional PET sheet production process, 
						reducing 67 per cent of energy consumption. With the 
						entire energy requirements derived from clean combustion 
						natural gas, the specially adapted burners consume up to 
						20 per cent less gas. As a result, the company’s PET 
						sheet, DPET™, boasts a 25 per cent lower carbon 
						footprint than conventional APET.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-10.jpg" width="300" height="192"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						In a recent study of the annual energy consumption using 
						MTR technology, it has been verified that OCTAL consumes 
						63.3 per cent less grid electricity and 22.7 per cent 
						less heat energy for the production of PET resin when 
						compared to conventional bottle grade resin. A total of 
						51.08 million kg of CO2 equivalent, which translates to 
						flying a full plane of 320 passengers around the world 
						approximately 39 times, was reduced.<br>
						<br>
						Additionally, due to the extreme precision and 
						consistency of the OCTAL’s DPET sheet, OCTAL’s customers 
						that shape and mould DPET into packaging products can 
						use up to 6 to 12 per cent less material by weight than 
						competing products, leading to further reductions across 
						the production chain.<br>
						<br>
						Hundred per cent of the water used by OCTAL is derived 
						from urban waste water and 80 per cent of the production 
						water is recovered and recycled in the manufacturing 
						system or used to irrigate the green belt of 
						predominately native plants around the facility.<br>
						<br>
						<b>A concerted effort<br>
						</b><br>
						Al Hassan Group of Companies which won the special 
						commendation in Green Footprint Award effectively 
						implemented the principles of ‘Reduce, Reuse and 
						Recycle’ in its daily business processes<br>
						<br>
						Al Hassan Group of Companies was able not only to 
						improve the surrounding environment but also to 
						simultaneously streamline the company’s business 
						processes and translate them into significant savings.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-11.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						To implement the green initiatives, a special energy 
						conservation group was formed by volunteers from various 
						group companies and project sites consisting of top, 
						middle and junior management which met on a regular 
						basis every two months. All 5000 employees of Al Hassan 
						Group are involved in the Safety week celebration from 
						February 25 to 29, 2012 which included energy, water and 
						environment conservation initiatives. By creating 
						awareness across various departments, the group was able 
						to achieve reduction in electricity charges, savings in 
						water bills and fleet fuel charges as well as vehicle 
						repair and maintenance charges. The landmark achievement 
						is the reduction in carbon footprint to the tune of 2555 
						metric tonnes on an annual basis. As part of its tree 
						plantation efforts, more than 3200 plants/trees are 
						being nourished for plantation at A’Seeb STP project to 
						give an environment friendly sewage treatment plant. 
						Some of the carbon footprint and carbon emissions 
						reducing steps included pumping of water instead of 
						trucking through water tanks. This reduced carbon 
						footprint by 752 metric tonnes. <br>
						<br>
						A reduction in carbon emission was seen owing to 
						virtualisation initiative taken by Data Centre which is 
						equivalent to planting of 197 trees.<br>
