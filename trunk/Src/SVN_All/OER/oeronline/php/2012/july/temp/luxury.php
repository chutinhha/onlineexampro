				products apart from the rest. Evolve Lifestyle showcases all the 
				contemporary collections as well as the classic traditional 
				creations from Hilker, including its huge range of designer 
				leather sofas and home theatre sofa systems. “Most of the Hilker 
				sofas come with various features such as adjustable arm rest, 
				back rest, head rest and reclining back,” informs Takkiar.<br>
