				The past few years have seen tremendous growth in Oman’s 
				household furniture market driven by the entry of a number of 
				real estate developers and increasing demand for residential 
				property. However, the premium furniture industry of the country 
				still continues to remain untapped, as there is an apparent 
				demand-supply gap forcing many homemakers in Oman to travel to 
				Dubai to pick up their favourite lifestyle furniture brands.<br>
				<br>
				An ambitious but meticulous stride towards filling this void in 
				Oman’s furniture market was the recent opening of Evolve 
				Lifestyle furniture showroom in Wattayah by Habitat Furnishing, 
				part of Al Jassar Group. Apart from the high-end leather sofas 
				from Hilker, Evlove also showcase a premium collection of finest 
				quality fabric sofas from far-east countries.<br>
