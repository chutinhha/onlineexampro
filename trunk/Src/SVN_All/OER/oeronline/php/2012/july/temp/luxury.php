				<br>
				Explaining the concept behind such an exclusive outlet for 
				premium lifestyle furniture, its business head Sarvajit Takkiar, 
				comments, “We wanted to bring something new and exclusive for 
				the people in Oman so that no one has to go out of country 
				seeking for up-market products. We strongly believe that there 
				is a huge potential in Omani market for such products and it’s 
				only a matter of taking an initiative for setting new trends, 
				instead of just being another furniture showroom in the city.”<br>
				<br>
				Evolve Lifestyle is a store for all those who want to purchase a 
				sofa with an excellent quality of craftsmanship and exclusive 
				design at unbelievably reasonable price. “Interior designers 
				always share their experience about how hard it is to get sofas 
				in a variety of designs and colours in this market; but we will 
				surely not disappoint them when they visit our showroom as we 
				have a perfect blend of quality, designs and colours in our 
				sofas,” adds Takkiar.<br>
