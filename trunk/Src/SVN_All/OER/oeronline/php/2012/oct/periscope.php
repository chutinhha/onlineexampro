						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Overvalued debt and currencies in global markets<br>
						</font><font face="Verdana" size="2">
                		<br>
						Sterling, the Aussie dollar, ten US Treasury notes, ten 
						year German Bunds, ten year South Korean notes and two 
						year German notes are all expected to be overvalued<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						The smoke signals coming out of the central banks will 
						determine the fate of the financial markets. Now that 
						the ECB’s latest unlimited bond purchase plan reduces 
						tail end risk of a Euro break up, sterling’s safe haven 
						bid against the Euro will vanish. This means sterling 
						will depreciate against the Euro and eventually against 
						the dollar, as global central banks reduce their gilt 
						(UK government) debt holdings. The Bank of England’s MPC 
						has also opted for monetary easing, with another &#8356;50bn 
						quantitative easing in July.<br>
						<br>
						Since the Tory-Lib Dem coalition in Downing Street will 
						not abandon austerity as the fundamental pillar of its 
						economic strategy, fiscal tightening will combine with 
						monetary easing to weaken sterling. While the ultra 
						dovish Bernanke Fed will temporarily boost the sterling 
						this autumn, the monetary feud between the ECB and the 
						Bundesbank, Spain’s de facto Catalan/Basque fiscal 
						revolt and the troika’s austerity diktat on Greece will 
						keep the European debt crisis simmering and trigger 
						periodic sterling falls. Britain, after all, is in 
						recession and CPI fell to 2.8 per cent in June. This 
						means there is a zero prospect of a bank rate hike in 
						either 2012 or 2013. The draconian spending cuts in the 
						2012, the budget unveiled by Chancellor Osborne and the 
						-2.7 per cent current account balance virtually 
						guarantees a lower sterling. My call? Cable falls from 
						1.60 to 1.52 in the next six months.<br>
						<br>
						<b>Epic fall in Aussie dollar<br>
						</b>The money printing sprees by the ECB and the Federal 
						Reserve have boosted the Australian dollar above 1.06 
						(and gold above $1780) but the economic metrics Down 
						Under now reflect the end of the mining boom. GDP growth 
						could well fall below 3 per cent, the collapse of iron 
						ore prices in August is a shock to Australian exports 
						and BHP Billiton has canceled a $20bn mining project. 
						Canberra does not want or need a super-strong Aussie 
						dollar, the reason the RBA will be forced to ease its 
						policy rate by at least 50 basis points in the next 
						three months. This could well trigger an epic fall in 
						the Aussie dollar to as low as 0.98 as it is simply not 
						priced into the global foreign exchange market.<br>
						<br>
						The ECB unlimited bond purchase programme, the German 
						Constitutional Court approval of ESM and the Bernanke 
						Fed’s perma-dovish monetary policies remove existential 
						tail risk for the Euro and the historic European 
						project. This is the kiss of death for the US Treasury 
						debt market. Uncle Sam’s risk free(?) debt is now the 
						most overvalued asset class in the world. As safe haven 
						capital flows reverse, the yield on the US Treasury note 
						can well rise to at least 2 per cent in the next three 
						months. Relative to equities, the CRB index, emerging 
						markets debt spreads, housing, inflation and even PPI 
						indices, ten year US Treasury notes are a screaming sell 
						at a 1.65 per cent yield. GCC investors have scrambled 
						into US Treasury debt as a hedge against Euro risk. This 
						trade was profitable as long as US Treasury yields fell 
						to 50 year lows. This trade will now hemorrhaging money 
						if it is not reversed. In fact, I would buy an inverse 
						bond index fund to make money from rising US Treasury 
						bond yields and a steepner UST yield curve, which is 
						hugely bullish for American money centre banks J.P. 
						Morgan, Citigroup and Bank of America. German Bunds are 
						a similar short. The FOMC move is extremely bullish for 
						mortgage REITs listed on the NYSE.<br>
						<br>
						I expect the Japanese yen to depreciate as US Treasury 
						bond yields rise and so does JGB spreads/US swap rates. 
						Deflation still haunts the Bank of Japan, which will 
						continue to pump liquidity into the Tokyo yen money 
						market (QE, like Shakespeare’s roses, by any other name, 
						smells such as sweet!). The Japanese yen can fall to 80 
						– 82 against the dollar and at least 106 against the 
						Euro.<br>
						<br>
						The South Korean debt market is another overvalued niche 
						in Asian bonds. South Korean steel exports to China will 
						continue to fall and the won yield curve is far too 
						flat. I expect the ten year South Korean won note to 
						rise at least 30 basis points in the next three months. 
						Another compelling macro trade is to short two year 
						German government notes, which offer a negative yield of 
						4 basis points. The only reason investors are prepared 
						to sacrifice a return and actually pay 4 basis points 
						over two years for Deutschland AG is because they expect 
						to be paid in Deutschemarks, not Euros. These 
						expectations will be unwound as the ECB, the Reich 
						Chancellery in Berlin and the Constitution Court has 
						basically eliminated Euro kaput risks. I expect the 
						German two year note yield to rise to positive 75 basis 
						points, its level in August 2011.<br>
						<br>
						To recapitulate, sterling, the Aussie dollar, ten US 
						Treasury notes, ten year German Bunds, ten year South 
						Korean notes and two year German notes are all 
						overvalued in my global macro crystal ball. The 
						financial markets can and do print money making ideas in 
						real time but the quest for value must determine every 
						investment decision!&#8194;<br>
