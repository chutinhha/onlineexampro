						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						In what can be termed as two crucial strides in telecom 
						indsutry, Omantel entered into an agreement with 
						Etisalat to expand Global MPLS connectivity, and teamed 
						up with Batelco to launch MPLS IP VPN interconnectivity 
						between Oman and Bahrain</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Expanding MPLS connectivity</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Etisalat and Omantel signed an agreement which will allow both 
				operators to expand their Global IP VPN footprint. The agreement 
				which was inked recently at Omantel headquarters in Muscat will 
				enhance the connectivity options for corporate customers between 
				the UAE and Oman. <br>
				<br>
				The two leading telecom service providers joined forces to 
				commence the installation of Etisalat’s Global MPLS (Multi 
				Protocol Label Switching) node in Oman. The project once 
				completed will enable Etisalat’s customers to connect their 
				offices on Etisalat’s backbone between UAE and Oman networks. 
				The agreement with Omantel will enable both companies to deliver 
				greater coverage, seamless user experience and reliable 
				technology to their respective customers.<br>
				<br>
				“Etisalat is delighted to announce the collaboration with 
				Omantel in expanding our footprint of Global MPLS services,” 
				said Abdulla Al Ahmed, senior vice president – business at 
				Etisalat. “This will also help us capitalise on the strong and 
				growing economic ties between Oman and the UAE to offer 
				end-to-end connectivity solutions to corporate customers in both 
				countries. Businesses can now rely on our mutual partnership to 
				support their mission-critical applications through a seamless 
				network for enhanced convenience and high quality of service 
				standards.”</font><p align="justify" style="margin: 3px 10px">
				&nbsp;<p align="center" style="margin: 3px 10px">
				<img border="0" src="images/inthenews-july-12-2.jpg" width="500" height="306"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				<br>
				“We are pleased to take our long-standing partnership with 
				Etisalat to new heights by the signing of this new 
				agreement,”said Sohail Qadir, vice president - Omantel wholesale 
				business. “The agreement will enable our growing number of MPLS 
				state-of-the-art service customers to connect with their offices 
				in both countries benefitting from the strong and reliable fiber 
				connectivity that links both countries and the international 
				reach of both respective operators.”<br>
				<br>
				<b>VPN interconnectivity with Bahrain</b><br>
				In another development, Omantel and Bahrain’s Batelco launched 
				MPLS IP VPN interconnectivity between Oman and Bahrain. The 
				partnership agreement was signed on the sidelines of the 
				International Telecoms Week (ITW) held in Chicago, US, by 
				Omantel’s senior manager of capacity and submarine at wholesale 
				business unit, Johannes Boersma and Batelco’s general manger of 
				enterprise division and government, Adel Daylami.<br>
				<br>
				Building on existing agreements, this partnership will allow 
				bi-lateral Data IP Services to be sold between Oman and Bahrain, 
				as well as providing one-stop-shop transit services to Omantel’s 
				and Batelco’s carrier partners, enabling both parties to extend 
				corporate VPN solutions between their respective markets. This 
				agreement will provide state-of-the-art, end-to-end MPLS VPN 
				services to Oman and Bahrain-based businesses that wish to 
				establish secure inter-office connectivity between both 
				locations. Through this agreement, Omantel and Batelco will 
				further strengthen its offering to carrier and corporate 
				customers in all GCC countries, Middle East as well as anywhere 
				around the globe. <br>
				<br>
				“Omantel provides a comprehensive range of managed services to 
				companies and carriers, and this new agreement with Batelco 
				enables Omantel to increase the depth of its MPLS VPN portfolio, 
				adding additional regional capabilities,” said Johannes Boersma. 
				“The company is proud to work with Batelco, which is a long-term 
				partner of Omantel.”<br>
				<br>
				“This strategic bi-lateral understanding is an important 
				addition to Batelco’s portfolio of OSS agreements,” said 
				Daylami. “Links for Batelco’s international customers and 
				partners can now be extended to all GCC countries through 
				Batelco’s Global IP VPN reliable, state-of-the-art network.”&#8194;</font></td>
