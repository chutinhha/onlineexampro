						<b>
						<font face="Verdana" size="2">
                		<br>
						Alizz islamic bank launched its RO40mn IPO to offer 400 
						million shares or 40 per cent of the total share capital 
						at 102bz per share. The subscription period will close 
						on October 21.</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-oct-12-2.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				The Initial Public Offering (IPO) of alizz islamic bank (under 
				formation) is now open for subscription. The IPO for 40 per cent 
				of the share capital comprises 400 million shares and is priced 
				at baisas 102 per share. On completion of the IPO, alizz islamic 
				bank will have a paid up share capital of 100 million rials. The 
				offer will close on October 21.<br>
				<br>
				Alizz bank is promoted by Huriah Company, an investment vehicle 
				owned by HH Sayyid As’ad bin Tarik al Said, UAE-based Aabar 
				Investments, Tasameem Real Estate Company and First Energy Oman, 
				which is a wholly-owned subsidiary of Bahrain-based First Energy 
				Bank. The promoters have already contributed RO61.2mn, 
				representing 60 per cent of the bank’s share capital at 102bz 
				per share. <br>
				<br>
				The IPO is lead managed by bank muscat and has been fully 
				underwritten by leading Omani financial institutions.<br>
				<br>
				Announcing the launch of the IPO on behalf of Huriah Management, 
				Ahmed Alkhonji said, “We are very proud to be supporting the 
				establishment of Islamic banking in Oman. The large unmet demand 
				for Islamic banking means that alizz islamic bank is uniquely 
				placed to become a market leader for banking products. We are 
				delighted to have Aabar, First Energy Bank and Tasameem as our 
				partners and the bank will benefit immensely from their 
				considerable international experience.”<br>
				<br>
				Coinciding with the IPO launch, alizz islamic bank also unveiled 
				its vision to be Oman’s leading Islamic bank that facilitates 
				commerce and development through innovative financial solutions 
				consistent with the principles of Islamic banking.<br>
				<br>
				“From a social perspective, the importance of the IPO cannot be 
				underestimated,” said Mohamed Al Fahim, board director of Aabar 
				Investments. “The products and services of alizz islamic bank 
				will help support the banking requirements of people in Oman, 
				within an appropriate cultural context for the first time. This 
				is key to continuing the remarkable growth and diversification 
				of Oman’s economy.”<br>
				<br>
				Chief operating officer Jamal Darwiche said that the bank aims 
				to capture a 20 per cent market share in Oman›s Islamic banking 
				sector over the next five years. “We expect to achieve a 3.5 per 
				cent market share in Oman’s banking sector and a nearly 20 per 
				cent share of the Islamic market by 2017. As per our 
				conservative projections, the bank is expected to make its first 
				profit in the second year of operations. We aim to become Oman’s 
				leading Islamic bank that facilitates commerce and development 
				through innovative financial products consistent with the 
				principles of Islamic banking.”<br>
				<br>
				Growth in Islamic banking has outstripped conventional banking 
				growth across GCC markets, and is now valued at around $300bn by 
				asset value. The promoters are confident that Oman will 
				experience similar growth in its new Islamic banking market and 
				that alizz islamic bank is uniquely placed to become a market 
				leader.&#8194;</font></td>
