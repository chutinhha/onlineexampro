				<b>
				<font face="Verdana" size="4" color="#C00000">Soaring high&nbsp; </font></b><p align="justify" class="style13">
				One of the best managed sectors, a pioneer in privatisation, 
				proven credentials and high investor interests... these are some 
				of the epithets often used to describe the electricity and 
				related water sector in the Sultanate. The sector stands out for 
				three primary reasons.<br>
				<br>
				First, the unbundling of the electricity sector and the passing 
				of the Sector Law as per the directives of Royal Decree 78/2004, 
				brought in a new market structure. The last seven years have 
				proven the strength of this regulatory structure which has 
				enabled semi-autonomous companies, with the right blend of 
				independence and responsibility, to deliver the goods. The 
				success of this exercise may prove to be a role model for others 
				to emulate. It is also a harbinger of the privatisation of the 
				sector.<br>
				<br>
				Second, the electricity sector has seen an improvement on almost 
				all parameters: Transmission and distribution losses are down, 
				Customer Average Interruption Duration Index (CAIDI) has 
				improved, state-of-the-art metres are being installed, supply 
				has kept pace with demand, customer service standards are better 
				and there is more accountability and transparency.<br>
				<br>
				Finally, having laid a foundation, the sector is now aiming 
				high. The capacity being added in the next two years rivals the 
				installed capacity of the last 40 years; alternative energy 
				sources like solar and wind energy are being tapped and an 
				integrated regional grid may soon be a reality. OER’s cover 
				story takes an in-depth look at the opportunities and challenges 
				facing the electricity in Oman.<br>
				<br>
				As Muscat Finance Company completes 25 years, we speak to a 
				cross section of its senior management to understand the reasons 
				of its enduring success. The two examples prove a singular point 
				– irrespective of whether it is the public or private sector a 
				combination of commitment, hardwork and values always pays.<br>
