						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Global leaders in petroleum resources</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>The six-nation Gulf Cooperation Council is the undisputed 
				global leader in energy resources, a fact that should provide 
				comfort to many stakeholders<br>
				<br>
				By Dr Jasim Husain Ali<br>
				<br>
				</b>The new edition of BP Statistical Review of World Energy 
				offered some surprises concerning the oil production of GCC 
				counties at large. Amongst others, Saudi Arabia has overtaken 
				Russia as the leading oil exporter in the world in 2011.<br>
				<br>
				Traditionally, Russia led the world in terms of oil output with 
				Saudi Arabia serving as the undisputed largest oil exporter. At 
				the moment, the kingdom enjoys supremacy in both production and 
				exporting of oil. However, Saudi Arabia has given way to 
				Venezuela with respect to the amount of proven oil reserves.<br>
				<br>
				Saudi Arabia alone accounted for 13.2 per cent of total oil 
				output in the world in 2011 against 12.8 per cent of Russia. The 
				other leading oil producers were the US, Iran and China by 
				virtue of accounting for 8.8 per cent, 5.2 per cent and 5.1 per 
				cent, respectively. In spite of their notable production levels, 
				both the US and China stand out for being leading key oil 
				importers to meet domestic demand.<br>
				<br>
				In retrospect, Russia and Saudi Arabia accounted for 12.9 per 
				cent and 12 per cent of global oil output in 2010, respectively. 
				The change serves as testimony of Saudi Arabia’s willingness to 
				compensate the international oil market from disruption of 
				output elsewhere such as Libya in 2011. The revolution in Libya 
				disrupted normal flow of oil for several months, but Saudi 
				Arabia in particular came to the rescue. <br>
				<br>
				In fact, major oil importers look up to Saudi Arabia to make up 
				for possible disruption to the international oil market caused 
				by sanctions imposed by the US and EU on Iran, the world’s 
				fourth largest oil producer. And much to the delight of Saudi 
				Arabia’s treasury, the tension over Iran’s nuclear programme 
				helps keeping oil prices relatively high, thereby providing an 
				added reason for steady output.<br>
				<br>
				Understandably, developments in the petroleum sector are paying 
				off in terms of ensuring substantial additional revenues. 
				Against this backdrop, Bloomberg has projected a notable 4.8 per 
				cent real inflation in Saudi Arabia’s economy in 2012. Within 
				the GCC economies, only the GDP of Qatar outgrows that of Saudi 
				Arabia. However, Qatar is noted for going on spending spree as 
				part of efforts to prepare the country for World Cup 2022.<br>
				<br>
				<b>Oman’s oil output <br>
				</b>Happily, steady rise on oil output and production 
				capabilities are not limited to major producers like Saudi 
				Arabia but others as well, notably Oman. According to the June 
				2012 issue of BP Statistical Review of World Energy, the 
				Sultanate’s oil production amounted to 715,000 barrels per day 
				(bpd) in 2007 only to increase to 891,000 bpd in 2011. <br>
				<br>
				The performance partly reflects the development at Mukhaizna oil 
				field. Looking back, Occidental of the US and its partners won a 
				concession back in 2005 to develop the field after committing 
				themselves to invest some $2bn, as part of efforts to increase 
				Mukhaizna’s production to 150,000 bpd. Clearly, the innovative 
				production sharing agreements are paying off well.<br>
				<br>
				<b>Oil reserves</b><br>
				Nevertheless, a marginal change has occurred last year with 
				regards to the location of proven oil reserves, with Venezuela 
				overtaking Saudi Arabia as the largest depositor of proven oil 
				reserves. According to the same source, Venezuela and Saudi 
				Arabia controlled 17.9 and 16.1 per cent of oil reserves in 
				2011, respectively. Nevertheless, Saudi Arabia enjoys global 
				leadership in both output and export of crude oil. <br>
				<br>
				Still, several other GCC countries are noted for enjoying 
				relatively strong deposits of global oil reserves, namely 6.1 
				per cent in the case of Kuwait and 5.9 per cent for the UAE. 
				Collectively, GCC countries control 494 billion barrels or 
				almost 30 per cent of total proven oil reserves in the world, 
				more so than any other entity. In fact, GCC states hold about 42 
				per cent of total reserves of Opec cartel, which includes oil 
				giants of Venezuela and Iran. <br>
				<br>
				The GCC region particularly matters to the well-being of 
				petroleum business given the production and exporting 
				capabilities. This partly explains the global interest in 
				maintaining peace and security in this part of the world. <br>
				<br>
				<b>Enormous gas deposits</b><br>
				Apart from oil, GCC states are noted for having significant gas 
				reserves. According to BP’s publication, Qatar’s share of 
				natural gas reserves stood at 25 trillion cubic metre or 12 per 
				cent that of the world. Only Russia and Iran possess more gas 
				reserves than Qatar, namely 21.4 per cent and 15.9 per cent, 
				respectively. <br>
				<br>
				But Qatar serves as the world’s largest exporter of liquefied 
				natural gas (LNG) by boosting a record 77 million tonnes per 
				year in 2011. Qatar’s LNG clients include Japan, South Korea, 
				India, Spain, the UK and the US, to name a few. <br>
				<br>
				Key international energy sources consider Qatar as the major 
				contributor of growth in LNG shipments in 2011. Yet, Qatari 
				authorities have decided to apply brakes on further increases of 
				LNG output in order to consolidate gains from existing output 
				levels. Certainly, Qatari authorities cannot overlook some 
				fundamental constraints including weak economic growth in EU 
				countries and the notable fallout of Greek’s debt problem. 
				Still, other bearings on gas consumption relate to purported 
				belief of relatively high gas prices, warm weather and growth of 
				renewable energy sources.<br>
				<br>
				Altogether, GCC member states Saudi Arabia and Qatar are world 
				players in the export of oil and gas, respectively, something 
				that provides comfort to concerned stakeholders like consuming 
				nations and oil traders is the fact that they are willing to 
				adjust output if and when needed.</td>
