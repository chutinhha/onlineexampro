						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">Best performers to be 
						felicitated in AIWA Awards </span><span class="style14"><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event-apr-12-2.jpg" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>The stage is all set for the 2012 edition of AIWA 
						Awards for Best Performing Companies. The nominees have 
						been finalized after an intense scrutiny of their 
						financial performance in 2011. AIWA Awards for Best 
						Performing Companies are instituted by Alam al-Iktisaad 
						Wal A’mal (AIWA), Oman’s leading Arabic monthly business 
						magazine, published by United Media Services (UMS). This 
						year marks the second edition of the AIWA Awards after 
						the highly successful launch in 2011. <br>
						<br>
						In a red carpet awards evening on 7 May, AIWA will 
						felicitate the top performing listed companies in Oman 
						during 2011. A networker’s dream, the event will be 
						attended by the who’s who of Muscat’s corporate world. 
						The premium event will attract more than 250 top 
						business leaders, CEOs and senior government officials 
						from a cross-section of industries, ministries and 
						government bodies. It will witness the congregation of 
						the key decision-makers who are driving the country’s 
						business and economy. A high-voltage entertainment show 
						by renowned international troupes will make the 
						attendees cherish the evening for a long time to come.<br>
						<br>
						Overall 15 Top Ranking Companies will receive the 
						coveted trophy. Based on the market capitalisation as at 
						end of 2011, the listed companies in Muscat Securities 
						Market (MSM) have been classified into three categories 
						namely large-cap, mid-cap and small-cap companies. The 
						AIWA Awards will be presented to the top five companies 
						in each category taking into account the defined set of 
						broader parameters including year on year (YoY) growth 
						of Revenue, Operating Profit (Earnings before Interest 
						and Tax - EBIT), Net Profit, Return on Average Assets (RoAA), 
						Return on Average Equity (RoAE), Operating Profit Margin 
						(EBIT) and the Net Profit Margin (NPM) of the qualifying 
						companies. In addition, AIWA will also felicitate two 
						prominent Omani nationals who have done the nation proud 
						by excelling in the fields of business and economy. The 
						AIWA Awards to the selected personalities will be made 
						in two categories – ‘AIWA Global Omani of the Year 
						Award’ and ‘AIWA Life Time Achievement Award’. AIWA will 
						also felicitate one company for its immense contribution 
						to the society and economy. It will be presented with 
						the trophy for ‘Excellence in Corporate Leadership’. 
						Gulf Baader Capital Markets (GBCM) is the knowledge 
						partner whereas KPMG is the validation partner for the 
						ranking process used for the awards.<br>
						<br>
						<b>Experts to debate on building national talent</b><br>
						When we talk of the major tasks before the nation, the 
						subject of Omanisation comes up as among the main 
						priorities. However, when we speak to the industry 
						leaders, they say the larges issue is building national 
						talent. ‘If we could work on creating a better talent 
						pool, the issue of Omanisation will be resolved 
						automatically’. However, it is easier said than done. 
						Keeping in mind the challenging task in hand, we need to 
						first understand: What are the gaps in terms of talent 
						demand and supply? What have been the reasons behind the 
						talent shortage and how we could bridge the gap? What is 
						going to be the contribution of the government, 
						education system, training institutions, civil society, 
						students and workers’ communities, business owners, top 
						managers, HR practitioners, etc.? Most importantly, all 
						these relevant stakeholders need to plan and work in 
						conjunction. <br>
						<br>
						That’s the objective of Oman Forum – Building National 
						Talent, an initiative of Alam al-Iktisaad Wal A’mal 
						(AIWA), Oman’s premier business magazine. Oman Forum – 
						Building National Talent is a half day long event 
						comprising two panel discussions on the state of 
						education &amp; training, youth employment policies &amp; 
						initiatives, identify talent gaps &amp; challenges and 
						create the guidelines to set up the road map for future. 
						To be organised on 17 April this year in association 
						with Petroleum Development Oman (PDO), OMAN FORUM – 
						BUILDING NATIONAL TALENT has been supported by the 
						Ministry of Higher Education, Ministry of Civil 
						Services, Sultan Qaboos University and The Research 
						Council (TRC). AhliBank, Voltamp, Galfar and Omran are 
						the Strategic Partners whereas Omantel and Oman Cables 
						Industry are the Associate Partners for the prestigious 
						forum. . KPMG is the Knowledge Partner and Times of Oman 
						and Al Shabiba are the Media Partners for the event. The 
						venue for the event is Al Bustan Palace Ritz-Carlton.<br>
						<br>
