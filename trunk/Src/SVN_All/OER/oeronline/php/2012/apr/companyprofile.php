						Bringing dog training to a new level</font></b><br>
				&nbsp;<p align="left" class="style13" dir="ltr">
				Canadian Jebel, Oman’s first full-fledged, registered dog 
				training services centre, offers a wide range of canine services 
				from basic obedience to intermediate and advanced levels of 
				training. An OER report<p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/companyProfile-apr-12-2.jpg" width="400" height="266" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				“An animal’s eyes have the power to speak a great language,” 
				famously said Martin Buber. Our relationship with dogs goes deep 
				in the history of human civilisation. Dogs are the only 
				domesticated animals that not only are a loyal companion, but 
				also perform a vast array of jobs such as guarding houses and 
				properties, sniffing out explosives, guiding people etc. The 
				loyalty and unconditional companionship provided by dogs have 
				proven to be even therapeutic, and researchers have countless 
				studies showing they can help to improve the quality of life for 
				many, by lowering stress, alleviating loneliness and improving 
				health through exercises.
						<p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/companyProfile-apr-12-1.jpg" width="400" height="266" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				Therefore it is not a surprise that keeping dogs as pets is on 
				the increase globally. The flip side is that as families are 
				working longer hours and travelling more often, dogs are 
				required to adapt and learn to cope. Although some dogs can 
				naturally adjust to new situations, many can develop behavioural 
				issues or bad habits or manners. As a result, more people are 
				seeking professional help to assist them in raising a new puppy 
				or resolving the behaviour problems of an adult dog. <br>
&nbsp;<p align="center" class="style13" dir="ltr">
				<img border="0" src="images/companyProfile-apr-12-3.jpg" width="400" height="266" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				Canadian Jebel, Oman’s first professional and registered dog 
				training services centre, offers luxury kenneling and 
				multipurpose training facilities for the canines in the 
				Sultanate. Run by Alfa Vold, a certified master trainer, 
				assisted by a well-trained staff, the facility provides a wide 
				range of canine services from basic obedience training to 
				intermediate and advanced levels of training. It is equipped 
				with spacious kennels with flaps that separate an outdoor run 
				from their indoor air-conditioned area. Other services offered 
				there include group classes, private training, and in-house 
				training in which dogs are taken for minimum two weeks to 
				provide training depending on the special requirements of their 
				owners.<br>
				<br>
				<b>Closer bond</b><br>
				“One of the best things about our line of work is the 
				opportunity to improve relationships– a closer bond is always 
				more easily attained once a relationship has formed, says Alfa 
				Vold. “We communicate through gestures, facial expressions, 
				eyes, posture; and when we do speak our tone of voice can 
				indicate our thoughts and moods. Dogs also understand this; they 
				communicate through their senses and body language and are 
				experts in reading us and communicate with us constantly.” <br>
&nbsp;<p align="center" class="style13" dir="ltr">
				<img border="0" src="images/companyProfile-apr-12-4.jpg" width="400" height="230" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				Vold says dogs are one of the best problem solvers of the animal 
				kingdom. “They learn from their experiences, through negative 
				and positive reinforcement. If they find a desired behaviour 
				benefits them, they quickly learn it is good for them to repeat 
				that behaviour. It is our job as their trainer/owner to guide 
				them to the correct response and to be liberal with our praise 
				and reward when they do so.” Vold also stresses that training 
				will bring a closer bond between the owner and the dog and that 
				dogs are usually less anxious and more content generally. <br>
