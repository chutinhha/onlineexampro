						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Catalyst for growth </b><br>
				<br>
				In 2011, the NBFC industry enjoyed a good run across various 
				segments, with all the six non-banking finance companies in the 
				country performing fairly well and posting good growth on the 
				back of new jobs, salary rises and infrastructure spending, 
				reports Muhammed Nafie<br>
				<br>
				Finance and leasing firms or non-banking finance companies (NBFCs) 
				have been crucial players in Oman’s financial services industry 
				for the last 25 years, contributing significantly to the 
				Sultanate’s economic development and diversified growth. Today 
				NBFC is a fast growing industry worth RO600mn and with large 
				capitalised companies who are so strong and resilient enough to 
				compete with banks at all levels. They have carved a niche for 
				themselves as the key supporters of small and medium scale 
				enterprises and retail financing in the country. <br>
				<br>
				Though the year 2011could not turn out to be a turnaround year 
				for national economy as expected, all the six NBFCs in the 
				country performed fairly well and posted good growth on the back 
				of new jobs, salary rises and infrastructure spending. The 
				industry enjoyed a good run across various segments, as almost 
				all the companies have performed well in their niche markets 
				wherein they may not be adversely affected by any kind of 
				slowdown at the macro level. In addition, the overall business 
				prospects are bright and positive for them in 2012, with 
				government’s expansionary fiscal policy and an expected 5 per 
				cent growth in real terms in 2012. “This is a matured market as 
				NBFCs have been in this country for over 20 years,” says Aftab 
				Patel, CEO, Al Omaniya Financial Services. “They are one of the 
				main sources of funds for the small and medium enterprises and 
				continue to be an important driver for growth. Therefore I think 
				they will remain as one of the strongest financial 
				intermediaries in the country.” <br>
				<br>
				The social and infrastructure spending in the budget for 2012 
				and the current five-year plan is very promising for the NBFCs. 
				“Responding to the social requirements of improving living 
				standards and creating new employment opportunities, the 
				government has increased the 8th five year plan expenditure from 
				RO43bn to RO54bn; and majority of this will be in oil and gas, 
				education, health, infrastructure etc.,”says Ajit Engineer, CEO, 
				Muscat Finance Company. “Established companies with proper 
				infrastructure, branch network, skilled manpower and strong 
				financials will be able to capitalise on these opportunities 
				coming up in the next five years. Leasing companies have a role 
				to play in a developing economy, especially in retail and SME 
				which banks would find difficult to address. All six players are 
				fairly capitalised, with reasonable portfolio and all are 
				profitable institutions. ”<br>
				<br>
				Mansoor Mubarak Al Amry, CEO, United Finance Company, 
				corroborates this saying, “Considering the magnitude of 
				infrastructure projects undertaken by the government, the mega 
				projects in the anvil and growing local youth population, the 
				market is expected to provide adequate potential for the 
				sustained growth of the non-banking finance industry in the 
				coming years.”<br>
				<br>
				Adds Robert Pancras, CEO, National Finance, “It has been our 
				experience that any injection of government funds and investment 
				into the economy is good news for the country. We are always 
				looking at and analysing business trends so that we are able to 
				react and offer new products that are clearly able to meet 
				demands from our customers. At heart we remain a 
				customer-focused business with the ability to quickly and 
				efficiently respond to the requirements of our growing customer 
				base.”<br>
				<br>
				Hira Lal Bharwani, CEO of Oman Oryx Leasing Company exudes 
				confidence saying that prospects are bright for NFBCs in Oman in 
				the future. “We are operating on our own niche. This year more 
				jobs were created in the private sector. This will create more 
				opportunities for leasing companies. Oman is much regulated from 
				the physical point of view. Government would not announce 
				projects without ensuring that it has all the proper funding 
				sources available. The increase of oil price coupled with 
				massive spending on infrastructure development across the 
				country augurs well for the development of SMEs and creates a 
				lot of opportunities for the companies.” <br>
				<br>
				Explaining about the prospects for 2012, Patel adds, “The 
				government has taken a lot of measures. A lot of investments 
				have gone into infrastructure development such as roads, 
				airports, ports etc. All of these have created a tremendous 
				amount of economic activity and demand. It will spur more 
				investment activities and the need for more capital and consumer 
				goods.”<br>
				<br>
				As Pancras rightly observes the underlying trends in the 
				non-banking finance industry remain very strong. There will be 
				solid growth in the economy that will stimulate demand for 
				products and in turn encourage individuals and businesses to 
				seek financing for various products. “It is clear that the 
				government’s economic strategy is reaping dividends with new 
				jobs and investment coming into the economy which is always 
				beneficial for businesses such as ours.” <br>
				However Bharwani admits that Omanisation has added on to the 
				operating cost of the company. “We continue to lose our trained 
				resources as people leave to government and banking institutes 
				and we are not getting trained people. The biggest challenge for 
				me is not running the business but retaining resources and 
				hiring staff.” <br>
				<br>
				“After three difficult years since 2008, it was expected that 
				2011 will be a turnaround year,” says Engineer. “But it was more 
				of a consolidation year. Although Oman is fairly insulated from 
				the vagaries of western economies, some effect will always be 
				felt. However, year 2012 has commenced promisingly, we are very 
				positive on the outlook as we are better positioned to 
				capitalise on the opportunities of economic development in 
				Oman.”<br>
				<br>
				Al Amry adds, “Considering the current market trend and the pace 
				of economic activity the outlook is promising though 
				challenging.”<br>
				<br>
				<b>Paid-up capital <br>
				</b>The year 2012 will also see the NBFCS gearing up to raise 
				their paid-up capital to RO20mn by June 30, in line with CBO 
				guidelines. Keeping this in mind, Al Omaniya has launched a 
				RO10mn bond issue. It will be 100 per cent compulsory 
				convertible bond of which 20 per cent will be converted into 
				equity each year. It is a five year bond. This will enhance the 
				capital base and net worth of the company and its ability to 
				grow. Patel feels that this measure is timely and structured 
				well to meet the CBO guidelines.<br>
				<br>
				National Finance has raised its incremental equity through a 
				rights issue of shares amounting to RO7.5mn to existing 
				shareholders which was completed in February 2012. “This has 
				incidentally increased our paid- up capital to RO25.54mn which 
				meets not only the first target of RO20mn by June 2012 but also 
				actually the next target of RO25mn by December 2016,” informs 
				Pancras.<br>
				<br>
				The board of directors of Muscat Finance has recommended a stock 
				dividend of 20 per cent for 2011, to fulfil the guidelines. This 
				will increase the company’s capital from RO16.80mn to RO20.16mn 
				by March 31. “And the board of directors will explore all the 
				possibilities and select the best way of raising it further to 
				RO25mn progressively over the next five years,” says Engineer.
				<br>
				<br>
				According to Bharwani, Oman Oryx which currently sits at 
				RO16.775mn recommended a rights issue worth RO5mn to the 
				existing shareholders which will helps the company to increase 
				the paid-up capital to RO21.775mn by June. <br>
				<br>
				To meet the requirements, Taageer Finance has offered rights 
				issue of RO4mn at par in May 2011 and the issue was fully 
				subscribed, according to the company’s annual report published 
				at Muscat Securities Market. The company also proposed to issue 
				stock dividend of 40 per cent by capitalising the share premium 
				reserve of RO3.091mn and the balance of RO1.909mn from the 
				retained earnings. <br>
				<br>
				<b>Foray into Islamic products <br>
				</b>Although the CBO is yet to issue the guidelines for NBFCs 
				regarding sharia-complied services, most of them are all set to 
				win a bigger slice of the burgeoning Islamic finance pie in the 
				Sultanate. “I don’t think the Islamic banks will affect the 
				profitability,” says Patel. “On the contrary, they will bring a 
				new dimension to the financial structure. It creates wider 
				choices for individuals and corporates. And as and when the 
				central bank issues guidelines, Al Omaniya, as the biggest NBFC, 
				will be there.”<br>
				<br>
				Muscat Finance is also waiting CBO’s regulations for Islamic 
				financing as it is positioning itself to launch the Sharia-complied 
				services. “Islamic banks are not likely to affect the 
				profitability of the NBFCs,” says Engineer. “Even the Islamic 
				banking has their own way of financing and generally in retail 
				and SME the leasing companies are better positioned. Despite the 
				presence of a number of commercial banks, we were still able to 
				make good progress and provide a handsome return to our stake 
				holders in the last 24 years. In addition, Islamic banks may be 
				a source of funding for us as we would be able to obtain funding 
				from them also.”<br>
				Bharwani says since leasing itself is an Islamic product, the 
				emergence of Islamic banking services holds a lot in store for 
				the leasing companies. “We have a sharia- complied derivative of 
				leasing which we have launched,” he adds. However Robert struck 
				a precautionary note saying, “The emergence of Islamic banking 
				is set to have an impact in the overall banking. But in a market 
				such as Oman it also appears that this will soak up a 
				considerable amount of untapped demand in the market. We 
				continue to remain focused on improving the basics, customer 
				awareness and unwavering focus on improving our quality of 
				services.”<br>
				<br>
				<b>At the top <br>
				</b>In 2011, Al Omaniya maintained its number one position among 
				other players in size, market capitalisation, net worth, 
				earnings per share, profit per employee, cash dividend, revenue, 
				the quality of assets etc.<br>
				<br>
				“Al Omaniya has emerged as the biggest nonbanking finance 
				institution in Oman sometimes back in 2008,” says Patel. “In 
				2011 also we maintained this. And our nonperforming loans are 
				lowest among all the banking and nonbanking finance companies in 
				Oman. Even when compared to the banks, ours, at just above 1 per 
				cent, is the lowest. We compete with the banks in almost all the 
				products such as working capital, project finance, bridge loans, 
				loans for construction of factories and warehouse, car loans, 
				equipment financing etc. In certain segments especially the 
				smaller SMEs, the leasing companies compete with us and 
				generally banks are not present in this segment.<br>
				<br>
				Patel ascribes this success to the way the company has 
				structured it business. “Our business model, strategies, 
				processes and systems, products and the way we deliver these 
				products and services all make a difference enabling us to have 
				the best possible customers. It has to be embedded in your 
				process, system, strategy and business model. It is only when 
				customers choose you in preference to other players, you can 
				emerge as a leader.”<br>
				<br>
				<b>A steady player<br>
				</b>For Muscat Finance, which celebrates the 25th anniversary in 
				2012, the year 2011 was satisfactory in terms of growth in 
				assets and profits. “We posted 4 per cent growth in assets and 6 
				per cent in net profit which went up to RO3.57mn,” says 
				Engineer. “The results were fairly satisfactory in a difficult 
				year. Though there were little disruptions in the first half, 
				the second half picked up well. Government’s focus on social 
				development helped in asset addition and created immense 
				financing opportunities for us.”<br>
				<br>
				Referring to the performance of the company vis-à-vis other 
				players in the industry, Engineer adds, “We have been a steady 
				player. We always had a conservative approach since inception 
				and grown our portfolio and profits consistently over the last 
				24 years. Although our assets are not the largest, many of our 
				ratios in terms of profit and return on assets and equity and 
				earnings are superior in the industry. <br>
				<br>
				However he believes that consistency is an important yardstick 
				to gauge the success of a company of Muscat Finance’s stature. 
				“We have an enviable track record of consistent profits and 
				distribution of dividends since inception. This year we are 
				entering the silver jubilee of our company. We are one of the 
				first two companies licensed in Oman in 1987. Our track record 
				is 25 years of consistent performance, a legacy that not many 
				competitors can claim.”<br>
				MFC has also managed to reduce its NPA substantially to RO9.7mn 
				from RO13mn. “Hopefully, it will reduce considerably in the 
				future. We have got an external consultant to look at our risk 
				policy and we have reviewed the risk parameters, tightened the 
				norms so that the quality of assets is improving.” He believes 
				that the ongoing infrastructure projects commenced earlier and 
				new contracts awarded in the last quarter of 2011 will open up 
				promising opportunities for MFC, as earthmoving equipment and 
				other construction and infrastructure related equipment will be 
				used in these projects. “In the silver jubilee year we have 
				rejuvenated ourselves. With a refreshing new logo and colour 
				theme and bold lines, we are looking to consolidate our 
				established brand,” avers Engineer.<br>
				<br>
				<b>On a sound footing<br>
				</b>The year 2011 was very important for National Finance as the 
				year when it passed RO100mn milestone in finance leases for the 
				first time. In addition, the year-on-year profit has grown by 
				52.1 per cent to RO3.546mn. Pancras says that the growth was 
				driven by a combination of increasing demand for car loans with 
				the expansion of the economy and consequent creation of jobs and 
				also increased customer acquisition in the rapidly growing SME 
				sector. <br>
				<br>
				“We are confident about the year ahead and the ability for our 
				business to achieve further growth – helped by growing demand 
				for our product portfolio; and what National Finance believes is 
				very solid and favourable conditions in the wider Omani 
				economy.”<br>
				<br>
				The total earning assets as at the end of 2011 amounted to 
				RO107.52mn of which the bulk pertained to finance leases; and 
				the company has substantially improved its working capital 
				assets. National Finance has three main sources of funds: 
				shareholders net worth, banks loans and corporate deposits. “The 
				third category deserves special mention as corporates with 
				surplus funds are increasingly finding us a more suitable 
				vehicle to park their surplus liquidity at attractive returns,” 
				adds Pancras. <br>
				<br>
				Referring to the NPA of the company which stands at RO6.5m, 
				Pancras says that ensuring efficient collections is a key 
				success factor in the business. “Given that we operate in a 
				riskier segment than banks, having a tried and tested 
				collections team which knows what to do is a tremendous comfort, 
				particularly in volatile times. Our portfolio performance over 
				the recent past has been one of the best in the peer group which 
				is a result of good credit decision making coupled with an 
				effective collections backbone.” <br>
				<br>
				<b>Record profit <br>
				</b>Oman Oryx has registered its best ever profit growth in 
				2011. “We have got many record numbers during the year,” says 
				Bharwani. “However, we have not been that aggressive and 
				continue to remain conservative as usual, as exemplified by our 
				finishing sixth in balance sheet; but in return on equity we 
				will be number three or four.” <br>
				<br>
				Bharwani attributes the rise in profitability to various 
				businesses started the previous year. “It was a good year 
				because the economy did very well. Our business is fundamentally 
				vehicles and commercials. All the new projects announced 
				recently will result in asset procurement in the market. So we 
				expect a good year.” <br>
				<br>
				In 2011, Oman Oryx’s operating asset was RO63mn with an increase 
				of 15 per cent. Its return on equity went up from RO12.11mn to 
				RO12.60mn whereas the infected portfolio went up from RO2.73mn 
				to RO2.848. <br>
				<br>
				Explaining the future outlook of the company, Bharwani adds, “We 
				are working on expanding our product range into softer loans, 
				medium term loans and some kind of guarantee business. But these 
				products are fundamentally riskier than pure core businesses. So 
				even if we have these products with us, it will give us kind of 
				an incremental business.” <br>
				<br>
				Bharwani says Oman Oryx has the lowest write-off in the market 
				and is number one or two in manufacturing portfolio and its 
				credit guidelines are very tight. <br>
				<br>
				<b>Consistency matters <br>
				</b>United Finance which commenced operations in January 1998 
				with a paid-up capital of RO5mn has expanded its presence across 
				the country with seven branches at Barka, Firq, Ibra, Ibri, 
				Mawaleh, Sohar and Salalah in addition to its head office in 
				Muscat. As of December 31, 2011, the company’s net worth stood 
				at RO34.9mn. The UFC has over the years increased its asset base 
				by growing its net finance debtor’s book to RO79.33mn. This was 
				backed by resource mobilisation through ploughing back of 
				earnings and prudent gearing. <br>
				<br>
				Says Al Amry, “UFC’s performance during 2011 was encouraging. 
				The company recorded a net profit of RO2.85mn for the year 2011 
				as against RO1.09mn in 2010. The loan portfolio also registered 
				a modest increase from RO72.32mn to RO79.33mn. The main factors 
				contributing to improvement in profitability during 2011 were 
				reduction in interest cost and overheads coupled with decrease 
				in impaired loans.”<br>
				<br>
				Referring to the measures to reduce non-performing loans and 
				curtailing disbursements in 2011, Amry adds, “Reduction in 
				non-performing loans was achieved through consistent efforts on 
				the recovery front coupled with tightening of credit norms to 
				mitigate the incidence of defaults from fresh loan 
				disbursements.<br>
				<br>
				However Amry admits that the UFC does not have any plan to 
				venture into new product areas. He also informs that going by 
				the prevailing market conditions the company does not expect any 
				appreciable increase in lending rates in 2012 if other factors 
				affecting interest rates remain the same.<br>
				<br>
				<b>Promising performance <br>
				</b>Taageer Finance has registered 8 per cent growth in the 
				lease portfolio during 2011 over the previous year, according to 
				the annual report. The profit after tax (PAT) for 2011 stood at 
				RO3.216mn with an increase of 30 per cent compared to the 
				figures in 2010. The report attributes this profitability to 
				decrease in borrowing costs and increase in gross income. Due to 
				general improvement of market conditions, the company maintained 
				its growth targets within the prescribed lending guidelines. 
				Taageer company continues the policy of creating and 
				strengthening the general provisioning and has provided 
				RO1.117mn towards loan impairments during the year. The 
				cumulative provision as at Dec 2011 stands at RO 4.649mn. <br>
				<br>
				In short, all the six NBFCs made significant gains in the last 
				fiscal year with solid and stable performance and started the 
				New Year on a very bright and optimistic note. <br>
