						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The coming correction in global equities<br>
						</font><font face="Verdana" size="2">
                		<br>
						There is a likelihood of disturbing macro storm clouds 
						for the S&amp;P500 index above 1400 <br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
				The year 2012 has witnessed a spectacular rally in global 
				equities, commodities and emerging markets. The Wall Street 
				S&amp;P500 index is up by 25 per cent since October. Emerging 
				markets like Brazil and Thailand have posted 35- 40 per cent 
				returns. Now even the GCC stock markets have surged led by the 
				near 30 per cent rally in Dubai’s DFM. However, I believe that 
				the bulls now exhibit the sort of classic “irrational 
				exuberance” that invariably ends in tears. The geopolitics of 
				the Middle East alone threatens another oil shock that could be 
				as devastating to the global economy as the October 1973 Middle 
				East war and the 1990 Iraqi invasion of Kuwait. Gasoline in some 
				American states is already $4 a gallon in a $15 trillion economy 
				where consumption is two third of GDP and household net worth 
				was gutted by the housing crash and two bear markets in the past 
				decade.<br>
				<br>
				The current bull run should be called the “Ben- Super Mario- 
				Masaaki” bull market because it was ignited by Ben Bernanke of 
				the Fed, Mario Draghi of the ECB and Masaaki Shirakawa of the 
				Bank of Japan. Their epic, unorthodox monetary policies slashed 
				interest rates and generated at least two trillion dollars in 
				high powered money that has inflated asset values in the 
				financial markets. The epicenter of the global bull market lies 
				in the US. However, I see disturbing macro storm clouds for the 
				S&amp;P500 index above 1400. Operating margins (ex Apple) have 
				fallen even in Silicon Valley, meaning a $7 hit on index EPS is 
				all too probable. The market cannot afford the luxury of 
				earnings volatility now that American shares have doubled since 
				March 2009 and the Chicago Volatility Index (VIX) has fallen to 
				a mere 15. The stock markets is overvalued and overbought even 
				as bond yields on Uncle Sam’s debt have begun to rise. Bullish 
				advisory sentiment is now at 48 per cent, near 2008 highs. 
				Geopolitics, Iran war risk, the reelection of President Obama 
				and fiscal tightening by the Republican Congress are all 
				negative for the indices.<br>
				<br>
				<b>Fading fast</b><br>
				Apart from central bank liquidity, the global markets have 
				attracted inflows from fund managers overweight cash who were 
				increasingly attracted by the unmistakable strength in US 
				economic data. After all, the economy has created 1.2 million 
				jobs in the past six months, the best performance since the 
				housing/credit bubble era of early 2006. Auto sales have surged 
				to an annual 15 million units. Even the monetary doves in the 
				Bernanke Fed have acknowledged the resilience in the US economy, 
				a major factor behind the spectacular bull run since October. 
				Yet the economic rationale for the bull market is fading fast.<br>
				<br>
				One, US ISM manufacturing, German factory orders, Premier Wen’s 
				slashing of Chinese GDP growth estimates to 7.5 per cent, the 
				ECB’s bleak forecast for Europe growth will lead to a U- turn in 
				macro sentiment in the next three months. This will be a 
				disaster for global investor sentiment.<br>
				<br>
				Two, Europe is the world’s leading economic and trading bloc, 
				the destination for 23 per cent of American exports, the source 
				of trillion dollar daisy chains of cross border bank credit to 
				Latin America, the Gulf, Southeast Asia. Europe faces not just a 
				recession but a classic banking credit crunch that will gut the 
				syndicated bank/offshore bond markets. The shock waves of the EU 
				credit crunch will hit exports and corporate earnings from China 
				to Brazil, from India to the GCC. The result? A wave of earnings 
				downgrades that will devastate sentiment in the world’s stock 
				markets this summer. This scenario is simply not priced into 
				current market valuations in either the US or the emerging 
				markets.<br>
				<br>
				Three, offshore fund managers have slashed their cash holdings 
				as they scrambled to accumulate equities. This is the most 
				dangerous point in the equity cycle since any hint of 
				disappointment in earnings or economic data means the risk of 
				significant sell offs.<br>
				<br>
				Four, the Chinese just posted their biggest monthly trade 
				deficit since 1989 (the year of the Tiananmen Square massacre, 
				not exactly an export friendly event!) as the European debt woes 
				hit the Middle Kingdom. The Chinese yuan forwards now suggest 
				the end of hot money inflows. The PBOC will cut the banking 
				system’s RRR at least thrice and slash interest rates. This will 
				be a negative backdrop for the high beta energy, metals and 
				industrial shares which have led the current rally. There are a 
				number of strategic trades that could make money for GCC 
				investors if my macro scenario unfolds:<br>
				One, the two year US Treasury note yield cannot rise much above 
				40 basis points if the US economic data deteriorates. The 
				Bernanke Fed will respond to weak data with stealth QE3. This 
				means the two year T note yield can plunge. Two, take profits in 
				GCC sukuk as credit spreads cannot compress when volatility and 
				risk metrics in emerging markets rise. Avoid all unrated or deep 
				junk sovereign debt. Reduce duration. Raise cash in GCC equities 
				markets.<br>
				Three, as economic weakness will raise deflation risk, gold 
				prices are headed lower. I would not be surprised to see gold 
				trade as low as $1500 an ounce. Four, the Indian rupee is headed 
				to the 53 – 54 range against the dollar. <br>
				<br>
				The Congress losses in the state elections, no credible retail 
				FDI or privatisation strategy, the impact of $125 Brent on the 
				current account/fiscal discipline, wholesale inflation and the 
				exodus of offshore funds from Dalal Street mean the Indian rupee 
				will depreciate significantly in the next six months.<br>
				<br>
				Five, now that the Greek bailout is over, the Swiss National 
				Bank will become more aggressive in its monetary policies. This 
				means the Euro/Swiss franc peg can well move from 1.20 to 1.26. 
				The Swissie is also grossly overvalued against the dollar. I 
				expect the Swiss franc to fall to 0.98 against the dollar and 
				the Aussie to 1.02.<br>
				<br>
				Six, the emerging markets index fund (symbol EEM) has risen from 
				34 in October 2011 to 44 now. Sell EEM at 44 for a 39 target. 
				The easy money in EEM must now be banked.<br>
