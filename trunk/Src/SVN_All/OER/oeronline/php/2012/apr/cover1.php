						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>The numero uno <br>
				</b><br>
				A combination of efficiency, prudence and healthy growth has 
				helped Ahlibank to emerge as a clear leader in the banking 
				sector. Abdulaziz Al Balushi, CEO, Ahlibank shares the winning 
				strategy of the bank in a candid chat with OER<br>
				<br>
				<br>
				&#8207;<b>Ahlibank has come up with a strong performance in 2011. Can 
				you share the highlights of your 2011 performance?</b><br>
				&#8207;Ahlibank had an excellent year not just in terms of the 
				bottomline, but in all areas of our business. We managed to 
				reduce our cost of funds and grow our quality assets. We 
				maintained our productivity at the highest levels in the country 
				and if you look at the return per employee, the bank which is 
				second is almost half of our figure. So our efficiency has been 
				very good. Our non performing assets (NPAs) have been maintained 
				at one per cent or below that level for the last five years, and 
				this is a great achievement. In terms of SMEs we have only three 
				accounts that are NPAs while in corporate banking we have zero 
				NPAs. So our quality of assets and our quality of earnings are 
				very good. Overall, we have had a very impressive bottomline. 
				Our balance sheet has grown impressively, to put things in 
				perspective since 2008 our loan book has grown by 3.25 times and 
				this growth has come from new businesses like corporate and 
				retail banking. <br>
				<br>
				&#8207;Talking of numbers, we have had a very good financial year. 
				Some of the highlights of 2011 are as follows:<br>
				&#8207;The bank’s net operating income has increased by 37 per cent to 
				RO35.31mn. <br>
				&#8207;Net profit after tax rose impressively by 29 per cent to 
				RO18.22mn <br>
				&#8207;Our loan book grew by 17 per cent to RO769mn <br>
				&#8207;Deposits growth by six per cent to RO669mn. This is because we 
				mobilised RO40mn through a subordinated debt issue in December 
				2010<br>
				&#8207;NPL ratio at one per cent, which is the lowest amongst banks in 
				Oman<br>
				&#8207;Our cost to income ratio was 30 per cent again the best by 
				industry standards<br>
				&#8207;The growth has been consistent, quarter over quarter and year 
				over year<br>
				&#8207;All our financial ratios and objectives have been met<br>
				&#8207;<br>
				<b>What are the measures taken by ahlibank to strengthen its 
				retail lending and corporate finance/project lending in the year 
				gone by?</b><br>
				&#8207;As we wanted to reduce our concentration risk we did not focus 
				on big projects in 2011. From the beginning of 2008 the gross 
				loan has gone up 3.25 times to RO 776mn. On December 31, 2007 
				this figure stood at RO239mn. Corporate loans have grown by 11.6 
				times to RO411mn in 2011 from RO35mn as of December 2007. Retail 
				loans have gone up by 1.8 times at the end of 2011 to RO365mn 
				from RO204mn on December 31, 2007. The bank achieved a personal 
				corporate loan ratio of 47 per cent as of December 31, 2011. &#8207;<br>
				<br>
				The above was achieved in a prudent way with established robust 
				risk management policies which is reflected in the quality of 
				assets (NPL ratio of one per cent as of 31-Dec-2011). The retail 
				growth was achieved due to an increase in the branch network and 
				a dedicated sales force. We have had several promotional schemes 
				for the government and quasi-government sectors. &#8207;The bank’s 
				process control have helped to reduce turnaround times and 
				improve customer service. Our enhanced net-worth and risk 
				participation with our strategic partner has enabled us to 
				finance some large projects during the year.<br>
				&#8207;<br>
				<b>The CBO has permitted Islamic Banking in Oman, what impact 
				will this have on the Sultanate’s banking sector and what are 
				ahlibank’s plan in this direction?</b><br>
				&#8207;Islamic banking will be another choice being offered to 
				customers who would wish to practice individual faith and 
				belief. The potential would be to attract customers who are 
				currently not availing any banking facilities due to lack of an 
				alternate option as well those who for similar reasons have 
				invested their funds elsewhere in the GCC. Global predictions on 
				the growth of Islamic banking in the years to come are very 
				encouraging and Oman would have its fair share of business. &#8207;The 
				CBO has granted Islamic Banking licenses to two new banks which 
				will be wholly Islamic and it is expected that most local banks 
				will have Islamic banking windows. Competition amongst local 
				players is bound to increase and there will be some advantage 
				for the first comers to this new business. &#8207;Ahlibank’s strategic 
				partner has developed expertise in Islamic banking and the 
				transfer of this to Ahlibank is almost complete. We have made 
				quite a lot of progress in preparing for the launch of Islamic 
				banking and these are as follows: <br>
				&#8207;A Shari’a Board has been constituted<br>
				&#8207;We have launched our Islamic banking logo and brand<br>
				&#8207;Core banking system to support Islamic banking activities have 
				been implemented<br>
				&#8207;Key personnel have been appointed<br>
				&#8207;Locations for dedicated branches identified and branch 
				infrastructure is complete at four locations<br>
				&#8207;Staff training has been ongoing and there are more than 25 
				existing staff who have been certified as Islamic bankers by the 
				Bahrain Institute of Islamic Finance<br>
				&#8207;Capital planning done and we are ready to launch as soon as the 
				CBO rules are announced <br>
				<br>
				&#8207;<b>Can you share details about your branch and ATM network and 
				are there any plans of upgrading your presence in the year 
				ahead?</b><br>
				&#8207;We currently have 12 branches and 17 ATM’s ( 5 offsite). The 
				bank is planning to increase the number of branches to 19 and 
				ATM’s to 26 (7 offsite ATMs). Of the 19 branches, six branches 
				will be dedicated to Islamic banking. Technology upgradation 
				will be ongoing and we will increasingly use the same to develop 
				our reach in Oman e.g. E-banking, SMS banking, phone banking, 
				ERP solutions etc.<br>
				<br>
				<b>Were there any measures taken by the bank to strengthen its 
				HR practices in 2011?</b><br>
				&#8207;We took a number of HR initiatives in 2011. A comprehensive HR 
				policy is in place. Staff benefits have been reviewed and many 
				social benefits introduced e.g. medical, hours of work, 
				education policy, maternity benefits etc. A fair meritocratic 
				assessment of staff through a improved performance appraisal 
				programme has been institutionalised. <br>
				<br>
				Training Needs Analysis for the entire staff has been completed 
				and training programmes have planned at all levels. The bank has 
				signed up with the CBFS for tailor made programmes exclusively 
				for our staff. A management trainee programme has been 
				introduced. On the job training as well as external training has 
				been planned for the year. We have put in place a succession 
				plan. Attempts are being made to develop a local talent pool.
				<br>
				<br>
				<b>Graduated to new level<br>
				</b><br>
				Bank Dhofar is preparing for further business growth and 
				sustainability by virtue of implementation of its strategic five 
				year plan for 2013-2018 which will give it a fresh perspective<br>
				<br>
				<br>
				Over the years, BankDhofar has participated in almost all the 
				major infrastructural projects like Oman India Fertiliser 
				Company, Sohar Aluminum, Oman LNG, Oman Gas, Salalah Port, Oman 
				Refinery (expansion), Octal Petrochemicals and six power and 
				water projects. Now the bank has graduated to the level of a 
				lead arranger from modest beginnings and has in place the 
				technical capability to independently arrange funds for medium 
				sized ventures with local, regional and international banks.<br>
				<br>
				The prognosis for 2012 is one of cautious optimism despite the 
				continuing global problems. This is on account of the 
				government’s bold initiatives to forge ahead with the 
				strengthening of the infrastructural framework of the country 
				and the likely scenario of sustained higher prices of crude oil.<br>
				<br>
				<b>Strategy and planning</b><br>
				With its five year strategy plan coming to an end in 2012, 
				BankDhofar has partnered with Boston Consultancy group (BCG) a 
				leading management consultancy firm to develop its strategy for 
				the future. The plan will cover major business units including 
				retail banking and wholesale banking. It will focus on 
				profitable growth while improving service, quality and overall 
				operations of the bank. In 2011, on the back of a sharp increase 
				in crude prices and a moderate increase in crude oil production 
				Oman’s budget surplus was beyond the projected level. The credit 
				off take in 2011 for the banking sector in general was moderate 
				at best and the Wholesale Banking Group (WBG) of BankDhofar 
				which caters to the needs of the corporate clientele performed 
				reasonably well. <br>
				<br>
				<b>Wholesale banking<br>
				</b>The WBG consists of three lending arms, namely, large 
				corporate banking department; project finance &amp; syndications 
				department and mid segment corporate department.The bank has 
				introduced customer segmentation based on certain quantitative 
				criteria for a specialised approach. The customer profile spans 
				across various industries which includes trading, manufacturing, 
				services and contracting.<br>
				<br>
				Despite a sluggish environment the asset growth in the large 
				corporate banking has been good chiefly on account of the 
				efforts put in by the team to target new clients and build on 
				existing relationships. Emphasis was also placed on maintaining 
				asset quality through detection of incipient sickness and taking 
				pro-active steps to mitigate the same. There was pressure on 
				interest margins due to increased competition. Non fund based 
				income which is mainly the commissions derived from issuances of 
				various guarantees and letters of credit was on a low keel due 
				to the slowdown in the award of new contracts.<br>
				<br>
				For the project finance and syndications department, the 
				Eurozone crisis had further exacerbated the availability of long 
				term dollar funds that was already under strain after the 2008 
				global economic crisis. Due to this none of the local banks had 
				the competitive edge to finance the latest three independent 
				power and water projects of Sohar, Barka and Sur which have 
				sourced their entire long term loan requirement from a syndicate 
				of international banks. The challenge of long term dollar funds 
				was partly addressed for smaller projects with club financing in 
				rial omani terms and some innovative financial structuring. the 
				lack of a proper basis for benchmarking the long term interest 
				rates is a major impediment in the use of rial omani for term 
				financing.<br>
				<br>
				The mid sector corporate department caters to the needs of the 
				corporates at the smaller end of the scale. Recently, the small 
				and medium enterprises (SME) accounts which hitherto had been 
				dealt by the retail banking division were shifted to this 
				department. The bank’s products and services are designed to 
				suit the particular needs of the smaller corporates and to 
				enable them to meet the challenges in the marketplace. In 
				consonance with the objective of the government to strengthen 
				the SME sector, BankDhofar is focusing on improving the credit 
				delivery to this segment.<br>
				<br>
				<b>Retail banking<br>
				</b>The retail banking division (RBD) of BankDhofar continued 
				its focus on providing value added products and services to the 
				customers. The bank continued its growth and expansion strategy 
				in 2011, strengthening its distribution channels with the 
				addition of more branches, ATMs and CDMs. The year 2011 saw the 
				addition of three new branches at Taqa, Mirbat and Bausher, the 
				year ended with 59 branches, 120 ATMs and 36 CDMs and one 
				multifunctional machine.<br>
				<br>
				During 2011, the product development team has worked on 
				enhancing existing products as well as designing, developing and 
				implementing new products and services. Last year, three 
				products and two services were launched. The products were 
				Bancassurance (Tamini), Al Heson new scheme and high yield 
				interest bearing deposit. The services were priority banking (Al 
				Riadah) and ladies banking (Hawa). In March 2011 bancassurance (Tamini) 
				was introduced at the branches and the official launch to the 
				public took place in July 2011. Tamini is a motor insurance 
				service provided by the bank through an insurance agency. The 
				insurance covers all kinds of policies and offers the lowest no 
				claim bonus discount in the market. High yield interest bearing 
				deposit has been revamped and re-launched in October 2011 to 
				offer the highest interest rate on savings and automatically 
				increases with balance increase.<br>
				<br>
				<b>Priority banking service<br>
				</b>Al Riadah priority banking service was launched in April 
				2011 with the aim of providing special services to customers 
				with a net worth of RO50,000 and above. Al Riadah offers the 
				very best of convenience and exclusivity to the bank’s priority 
				customers. There are dedicated relationship managers assigned to 
				process the customer’s transactions quickly, efficiently and 
				help them manage their everyday banking needs. There is also an 
				exclusive Al Riadah lounge in Azaiba. A special debit and credit 
				card was designed to allow higher daily withdrawals.<br>
				<br>
				<b>E-banking <br>
				</b>As part of the of the distribution plan for 2011, the 
				electronic channels witnessed a remarkable development which 
				improved service levels. Call centre services were also improved 
				through enhanced training and introduction of service monitoring 
				tools, such as mystery shopping and call quality scoring. The 
				call centre strength was increased to 16 call agents under a 
				manager. The call centre also generated an increased amount of 
				business through inbound and outbound campaigns. To ensure 
				customer satisfaction, a strict quality control procedure has 
				been implemented. In addition, the call centre staff is put 
				through periodic training programmes on products and customer 
				handling skills. In the period of last 12 months, the call 
				centre continued witnessing an increase in the number of calls 
				being handled by staff - about 176,527 calls were handled by the 
				staff in 2011.<br>
				<br>
				<b>New initiatives<br>
				</b>With a view to capitalise on the opportunities arising out 
				of the rapid development of Sohar as an industrial hub, efforts 
				are on to open a corporate branch there. There are also plans to 
				start corporate desks at high volume centres in Muscat such as 
				Azaiba and Rusayl. An exclusive call centre services for 
				corporate customers is also on the anvil. New services were 
				added such as third party transfers in ATMs and the opportunity 
				to donate to charities using CDMs. The bank is planning to 
				introduce banking kiosk to enhance customer service. There are 
				also plans to introduce MasterCard acquiring on the bank’s ATM 
				and introduction of MasterCard and Visa POS acquiring. ATM and 
				CDM network comprising of 157 machines will be expanded further 
				in the year 2012 to enhance the bank’s coverage.<p align="right" class="style13" dir="ltr">
