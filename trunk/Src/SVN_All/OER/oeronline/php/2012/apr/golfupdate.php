				Championship in Miami recently. Two weeks before players gather 
				at Augusta National for the first major tournament of the year, 
				Donald won a four-man play-off to take back the top ranking spot 
				he surrendered to McIlroy.<br>
				<br>
				The Englishman needed a win in Florida to reclaim top spot from 
				his Ryder Cup team-mate and he did just that by holding his 
				nerve to win a four-man play-off. Robert Garrigus, Jim Furyk and 
				Bae Sang-Moon were the players edged out by Donald after the 
				quartet had finished the fourth round on 13 under par.<br>
				<br>
				A run of five birdies in the first 11 holes moved the 
				35-year-old into contention, before he consolidated with seven 
				successive pars to ensure a 16th top-10 finish in his last 22 
				events. He hit an excellent approach shot on the first hole of 
				the play-off with a seven iron from the rough and holed the putt 
				to seal his victory.<br>
				<br>
				Hoping to finally capture his elusive first major title at 
				Augusta National next month, reigning US and European prize 
				money champion Donald took his tension-packed triumph in stride. 
				‘‘It’s another step in the right direction to winning majors,’’ 
				Donald said. ‘This is prefect preparation - two weeks off now 
				and looking toward Augusta.’’<br>
				<br>
				The playoff took place on the par-4 18th hole, with Donald the 
				only player to find the dense rough off the tee. He blasted his 
				approach six feet from the hole but Garrigus put his second shot 
				only seven feet from the cup. After Bae and Furyk missed long 
				birdie putts, Garrigus pushed his birdie bid just left of the 
				cup, setting the stage for a dramatic birdie putt that brought 
				Donald his first US playoff win.<br>
				<br>
				‘‘I certainly was a lot more nervous the first time trying to 
				get to No.1,’’ Donald said. ‘‘This time I was just focused on 
				winning the tournament and it all worked out.’’<br>
				<br>
				McIlroy was quick to praise his Ryder Cup team-mate, saying on 
				Twitter, “Well I enjoyed it while it lasted! Congrats LukeDonald! 
				Impressive performance!” <br>
				<br>
				Donald’s victory made it three European victories in a row in 
				America after McIlroy triumphed at the Honda Classic and Justin 
				Rose did likewise at the Cadillac Championship. Donald admitted 
				the win was a morale booster ahead of the Masters.<br>
				<br>
				After his record-setting year in 2011, Luke Donald has had a 
				slow start in 2012. Donald was the first player to ever win the 
				money titles on both the PGA and European Tours in the same 
				year. He had two wins and an amazing 14 top 10s in 19 starts on 
				the PGA Tour; plus, he added two more wins and six more top 10s 
