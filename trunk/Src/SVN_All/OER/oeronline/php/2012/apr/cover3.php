						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>On a strong footing</b><br>
				<br>
				Bank Sohar has been able to enhance its performance and 
				productivity apart from providing career opportunities for its 
				staff, says Dr Mohamed Abdulaziz Kalmoor, CEO<br>
				<br>
				<b>How was Bank Sohar’s performance in financial year 2011? <br>
				</b>Bank Sohar registered a net profit of RO14.5mn for the 
				financial year 2011, a growth of 42 per cent compared to 2010 
				despite a challenging economic and business environment 
				aggravated by the credit crisis in Greece and the downgrade of 
				the USA by a leading rating agency. The operating profit for the 
				year was over RO20mn, an increase of 28 per cent compared to 
				2010. Net interest income during the year increased by 13 per 
				cent to RO34.8mn during the year. The bank continued its focus 
				on the corporate sector to drive a risk free but profitable 
				business model. The operating income for the year 2011 increased 
				by 22.60 per cent in comparison to the previous year. This was 
				despite an increase in the cost of manpower as well as that of 
				infrastructure experienced by the whole country. Bank Sohar 
				continued its focus on improving efficiency in both its fund 
				management and its operations. The cost-to-income ratio improved 
				to 53.67 per cent in 2011, down from 55.6 per cent in 2010. 
				Significant gains were made in the bank’s overall business as 
				well. Net loans and advances increased by 12.9 per cent during 
				the year to touch RO1,020mn. Customer deposits grew by 17.2 per 
				cent to RO1,171mn during the year signifying their continued 
				faith in Bank Sohar.<br>
				<br>
				<b>What are the measures taken by Bank Sohar to strengthen its 
				retail lending and corporate finance/project lending in the year 
				gone by?<br>
				</b>Bank Sohar continues with its strategy of providing its 
				retail customers with loans for building productive assets 
				without substantially increasing their indebtedness. The retail 
				customers are provided with the entire suit of loan products 
				including home loans, personal loans, business loans, credit 
				cards etc. Bank Sohar through its wholesale banking group is 
				active in project financing for infrastructure and manufacturing 
				projects and other initiatives of national importance. The bank 
				participated in some large loans syndications during the year 
				and continues to pursue opportunities in the project finance 
				sector.<br>
				<br>
				<b>The Central Bank of Oman has permitted Islamic banking in 
				Oman, what impact will this have on the Sultanate’s Banking 
				sector and what are Bank Sohar’s plan in this direction?<br>
				</b>One of the key initiatives for Bank Sohar during the year is 
				the introduction of an Islamic banking window. In pursuance of 
				this goal, the bank is putting in place frameworks, systems and 
				procedures to facilitate the roll-out of Shariah-compliant 
				products and services. Competition in this emerging financial 
				sector is expected to be fierce, given the widespread interest 
				in Islamic banking evinced by Omani customers. Bank Sohar 
				however is determined to be a key player in this area. The bank 
				has also taken steps to raise its capital base by RO10mn via a 
				rights issue to capitalise on this initiative. <br>
				<br>
				<b>Can you share details about your branch and ATM network and 
				are there any plans of upgrading your presence in the year 
				ahead?<br>
				</b>Bank Sohar added four new branches during the year, 
				expanding the branch network to a total of 25 branches by end 
				2011. These additions in prime locations yielded fruitful 
				results for the bank in terms of additional business and have 
				lent convenience to customers. During the year, the bank also 
				added seven ATMs, taking the total count to 44 ATM machines 
				across the Sultanate. Bank Sohar customers continue to enjoy the 
				privilege of free-of-charge access to over 650 ATMs under the 
				“Oman-Net” platform. Bank’s ATMs at Buraimi and Wajajah borders 
				were activated with a “Dirhams Dispensing” facility; thus adding 
				immense convenience to businessmen who travel and transact 
				business across the border on a regular basis as well as to 
				customers who visit the UAE. Cash deposit facility at all branch 
				ATMs was added as another convenience offered to customers. With 
				the addition of this facility, the bank’s existing ATM machines 
				perform a dual function.<br>
				<br>
				The bank will continue to expand its network of branches and 
				ATMs to improve its reach and to facilitate customers’ access to 
				the bank’s touch points.<br>
				<br>
				<b>Were there any measures taken by the bank to strengthen its 
				HR practices in 2011?<br>
				</b>The main focus for 2011 was to enhance performance and 
				productivity and to provide employment and career opportunities 
				to the bank’s internal staff. There is a continuous effort in 
				developing, motivating and engaging our team, something that has 
				succeeded in addressing the organisational objectives in the 
				continued challenging business environment. The bank also 
				achieved the milestone of 90 per cent Omanisation set for the 
				banking industry. <br>
				<br>
				The bank has initiated customer satisfaction survey for retail 
				as well as corporate customers. Outcome of the surveys has 
				helped us in evolving our own ‘External Customer Service Index’. 
				To measure how one department serves the other, the bank has 
				developed ‘Internal Customer Service Index’ through an internal 
				customer service survey. The third key parameter of customer 
				satisfaction is employee satisfaction which we have gathered 
				through an ‘Employee Opinion Survey’. <br>
				<br>
				The outcomes and results of all these surveys has given us clear 
				indications of how well we have progressed in our journey to 
				excellence and where improvements are required. To develop the 
				leadership at all levels in the bank, we have introduced a 
				Leadership Excellence Programme this year in a very structured 
				and professional manner. This was a key step that the bank has 
				taken in creating a ‘Culture of Learning’ which will help all 
				our employees to develop their skills and competencies to enable 
				them to play a leadership role that they need to play to take 
				this bank to greater heights and meet the future challenges that 
				lie ahead. This programme will be further enhanced across the 
				bank at different levels thus making learning a way of life in 
				Bank Sohar. <br>
				<br>
				Amongst many initiatives implemented during the year, we would 
				like to mention team briefing, enhanced performance management 
				system, staff appreciation and recognition (STAR) programme and 
				a competency based interview process. <br>
				<br>
				<b>Can you tell us about the bank’s road map for FY2012?<br>
				</b>The bank will continue with its vision of being a “One stop 
				financial super mall having boutiques of products and services 
				across various segments, each with a unique set of 
				propositions”. Moreover, it will always be on the look-out for 
				new business opportunities, and be alert to ensuing challenges, 
				and thereby endeavour to address them in a timely manner. <br>
				<br>
				<b>Capitalising on its strengths</b><br>
				<br>
				The introduction of innovative products and improved services 
				proved beneficial for National Bank of Oman’s customers, says 
				Salaam Al Shaksy, CEO. Excerpts from an interview <br>
				<br>
				<br>
				<b>How was NBO’s performance in the financial year 2011? <br>
				</b>The bank reported a strong growth during the financial year 
				2011. Net profit after tax increased by 26 per cent to RO34.2mn, 
				compared to RO27.2mn in 2010. Net interest income grew by five 
				per cent compared to 2010 while the cost of funds improved by 51 
				basis points (bps) during the year. Non-interest income grew 
				significantly and was 37 per cent of the total income. The cost 
				to income ratio improved from 51 per cent to 47 per cent even as 
				the bank continued to invest in its employees, systems and 
				delivery channel network. The bank continued its focus on 
				recoveries as well as reduction in non-performing loans. 
				Recoveries and release of provisions from credit losses 
				increased by 43 per cent compared to 2010. Non-performing loans 
				came down from 3.5 per cent in December 2010 to 2.94 per cent at 
				the end of 2011. The coverage for loan loss provisions stood at 
				100 per cent at the end of the year. Provision for credit losses 
				and investments for the year were higher mainly due to an 
				increase in the impairment of investments classified under the 
				available for sale category. Customer lending during the year 
				increased by 23 per cent to reach RO1.7bn. Customer deposits 
				grew by 21 per cent over the previous year to close at RO1.6bn 
				as at December 2011.<br>
				<br>
				<b>What were the measures taken by NBO to strengthen its retail 
				lending and corporate finance/project lending in the year gone 
				by?</b><br>
				The bank’s retail performance in 2011 was excellent. A number of 
				innovative products were introduced and service quality across 
				all delivery channels was improved. NBO’s housing loan scheme 
				‘Al Manzel’ was completely redesigned with many new features and 
				offers. Apart from attractive interest rates and improved 
				service, a fall in property prices to reasonable levels and an 
				increase in salary levels and other benefits, enabled NBO to 
				aggressively market Al Manzel amongst potential borrowers. We 
				are currently offering one of the lowest interest rates to our 
				customers along with longer tenors of up to 30 years and an 
				option to pay insurance fees on a monthly basis rather than 
				paying this upfront. <br>
				<br>
				With the recent increase of salaries in many sectors coupled 
				with the prudent policies being pursued by the Central Bank of 
				Oman (CBO), the bank’s personal loans portfolio has also shown a 
				modest increase. We are also taking cautious and measured steps 
				towards extending credit to small and medium sized 
				establishments, aiming to generate self-employment and 
				strengthen the economy further. Undoubtedly, we envisage immense 
				potential for this segment of clients and thus are formulating 
				plans to penetrate this segment further. <br>
				<br>
				Additionally, 2011 was a very good year for the NBO project and 
				corporate finance portfolio and we were able to garner more than 
				30 per cent of the loan growth in this segment. We also led the 
				financing for major transactions like aluminium rolling mills 
				for Takamul and were the leading local bank on Sur IPP deal. The 
				success was largely attributable to our holistic wholesale 
				banking approach to clients where we offer a one stop shop for 
				advisory, risk management, transaction banking and corporate 
				finance needs. It helps us engage our clients at a more 
				strategic level and better understand their needs. Our ultimate 
				goal in each relationship is to be the preferred supplier of 
				choice and a trusted advisor. Our clients have appreciated our 
				approach and we are pleased to see the results in terms of our 
				marketshare and perception. <br>
				<br>
				<b>The CBO has permitted Islamic banking in Oman. What impact 
				will this have on the Sultanate’s banking sector and what are 
				NBO’s plans in this direction? <br>
				</b>While the announcement of establishing two Islamic banks in 
				the Sultanate is expected to increase competition in a banking 
				sector which seems to be showing signs of saturation, the move 
				could also help attract funds that may have flowed out of the 
				country in search of Sharia-compliant products. This, in 
				addition to the injection of fresh capital for the establishment 
				of Islamic banks and windows, should boost the ability to meet 
				the country’s growing financing needs. NBO aims to launch an 
				Islamic banking window this year and will provide Sharia-compliant 
				products and services to both retail and corporate customers. 
				The bank will adhere to the highest sharia-compliant measures 
				and also keep the business lines clearly segregated as per the 
				CBO regulations.<br>
				<br>
				<b>Can you share details about your branch and ATM network and 
				are there any plans of upgrading your presence in the year 
				ahead? </b><br>
				We have a presence across the country via 69 branches and 181 
				ATMs. The substantial expansion plans since 2007 have now 
				culminated in 20 new branches with an equal number of new ATMs 
				in key strategic locations. We are constantly upgrading our 
				branch network as this remains the edifice on which a successful 
				retail bank can run. Similarly, electronic channels are an 
				integral part of our suite of offerings and we have ambitious 
				plans to strengthen them. We are planning to increase our ATM 
				network along with Cash Acceptance Machines. These new machines 
				will accept, as well as dispense cash, and will be placed in key 
				branches/areas, benefiting many customers who would like to 
				deposit their cash post banking hours.<br>
				<br>
				<b>Were there any measures taken by the bank to strengthen its 
				HR practices in 2011?<br>
				</b>In keeping with the bank’s corporate strategic objectives to 
				recognise the importance of staff, the road map for undertaking 
				Human Resources Strategic Transformation (HRST), was 
				conceptualised in 2011. The entire process began with an 
				initiative to build faith in HR and to ensure seamless 
				communication. The HR Help Desk was introduced and is carefully 
				monitored and analysed on a continuous basis. Under the umbrella 
				of the HRST, various other important activities were initiated. 
				They include:<br>
				Re-organisation of the HR Functional Structure<br>
				Salary review to benchmark against industry norms<br>
				Audit of HR practices<br>
				Revision and redesign of performance management system, HR 
				policies and grades and associated bands across NBO<br>
				In order to ensure a minimal communication gap between staff and 
				management, a bi-monthly HR-Labour Union Forum was established. 
				The formalisation of the HR Executive Committee was another 
				major achievement and an indication of management’s commitment 
				to focus on all aspects of staff matters and their wellbeing. In 
				recognition of the importance of our staff and our ability to 
				assist in fostering their development, a major overhaul of our 
				Learning &amp; Development strategy was also undertaken. <br>
				<br>
				The Phase 1 Human Resources Management System (HR Automation) 
				benefitted the organisation in ensuring smooth operation of 
				various HR processes related to payroll, leave management and 
				administration. The implementation of these modules realised 
				prompt and efficient services, accuracy, and optimisation of 
				resources with overall reduction in processing related paper 
				work.<br>
				<br>
				<b>Can you tell us about the bank’s road map for FY 2012? <br>
				</b>The bank is looking to sustain its growth momentum in 2012. 
				Our wholesale and investment banking is poised to take advantage 
				of the opportunities created by anticipated government spending. 
				Mortgage and small business lending, card products and low-cost 
				deposit activities will remain the focus of retail banking. 
				Keeping Oman as the country of primary focus, the bank will also 
				capitalise on the UAE franchise as an additional service point 
				for customers. Strong risk management will be a key focal point 
				as the bank embarks on implementation of its strategic plan 
				(established in year 2011), with regard to financial 
				performance, customers, products, processes as well as 
				employees. The bank also plans to launch its Sharia-compliant 
				offering during the year. <p align="right" class="style13" dir="ltr">
