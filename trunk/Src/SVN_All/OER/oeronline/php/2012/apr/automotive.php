						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Zubair 
						Automotive Group General Manager Serdar Toktamis looks 
						at how a healthy automotive sector in Oman is a positive 
						sign of economic growth and prosperity</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/automotive-apr-12-1.jpg" width="500" height="333" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<b>Healthy car sales= <br>
				healthy economy<br>
				</b><br>
				It is often said that the car industry can be one of the most 
				reliable barometers of economic prosperity and success. When 
				sales are up – it is a pretty sure sign that the economy is 
				moving ahead and there is a real air of confidence among the 
				consumers and of course the opposite can be just as true as 
				well.<br>
				<br>
				But in Oman, and increasingly across the whole of our region, 
				the barometric pressure is getting higher every day thanks to 
				new economic initiatives, well planned government investment and 
				the benefits of a healthy oil price. New job entrants into the 
				market, rising salaries and an underlying sense of confidence 
				are now actively stimulating the automotive sector for 2012.<br>
				<br>
				<b>Exciting region for global automotive manufacturers<br>
				</b>The Middle East region has seen some of the sharpest rises 
				in GDP growth in the world over the past decade and a 
				corresponding rise in personal wealth, that when combined with 
				low gasoline prices, has made the area an increasingly vital 
				market for manufacturers. Models are now being developed with an 
				eye on the Gulf, and Chrysler Group Middle East for example has 
				recently conducted thermal testing programmes specifically with 
				our region in mind as climactic conditions and vehicle usage 
				differs greatly here from any other market in the world. And in 
				a region where the bulk of the population is under 25 and with 
				tens of thousands of young drivers entering the market every 
				year, there is clearly enormous growth potential for the future 
				– a fact that is now clearly recognised by the major global car 
				manufacturers.<br>
				<br>
				The Middle East has traditionally been dominated by Japanese 
				manufacturers but the vital importance of the region is 
				reinforced by the very strong initiatives from both European and 
				US brands who are aggressively seeking ways to improve and 
				enhance their share of the market. I think there is little doubt 
				that we will seek fairly substantial gains from the US and 
				European manufacturers across the Middle East in the years 
				ahead.<br>
				<br>
				<b>Global events impacting global players<br>
				</b>The automotive industry of the 21st century meanwhile is a 
				truly global phenomenon. And that means no one is immune to 
				situations such as the global economic slowdown of recent years 
				or natural disasters such as the earthquake and subsequent 
				tsunami in Japan. As a highly interconnected business what 
				happened in Japan affected non-Japanese producers who could no 
				longer source specialist paint colours; for example impacting 
				producers in both Europe and the US, in addition to the direct 
				manufacturing issues that struck Japanese producers.<br>
				<br>
				It has also meant that as a global industry every major 
				manufacturer establishing local production facilities around the 
				world – with European brands such as Volkswagen for example 
				setting up facilities in China solely for Chinese demand and in 
				Mexico and the USA. It means supply chains are significantly 
				reduced, companies can benefit from well priced skilled labour 
				and it can also protect them against currency fluctuations.<br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/automotive-apr-12-2.jpg" width="500" height="333" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
				<b>Vibrant automotive market<br>
				</b>When you consider that just over 40 years ago there was less 
				than 20 kilometres of paved road in the Sultanate, it is quite 
				extraordinary how the infrastructure and modern highway system 
				has grown and developed in such a short space of time. What has 
				also changed dramatically over four decades is the profile of 
				the Omani driver. Today an Omani in his mid 20s is looking for 
				something totally different to his father – who would have been 
				more conservative in his choice of vehicle opting for 
				practicality, family comfort and economy over style.<br>
				<br>
				In the internet age however young Omanis are now looking for 
				something highly individual – and they are influenced by design, 
				style and how the car looks – approaching it far more from a 
				sense of fashion and fun and wanting to drive away from the 
				showroom in a car that will turn heads across town. And it is a 
				trend that is being recognised by the big automotive 
				manufacturers who are increasingly introducing great looking 
				cars and SUVs into the market. Today the young Omanis and others 
				who come into our showrooms really do care what they drive – it 
				is no longer just an effective way to get from one place to 
				another. With information about brands around the world 
				available at the click of a mouse – it means customers come to 
				the showroom armed with facts, figures and statistics at their 
				finger-tips. And that in itself means the automotive sales teams 
				have to be better trained, more experienced and knowledgeable 
				about the cars and SUVs they sell than ever before.<br>
				<br>
				<b>Customer-centric future<br>
				</b>The automotive market in Oman will naturally become more 
				competitive – it is a simple fact of business life. And for a 
				distributor like us to enjoy continued growth and success in the 
				future the service relationship between the distributor and the 
				individual customer will be absolutely vital.<br>
				<br>
				I believe that prices in the future will not be the deciding 
				factor. Yes of course customers will still want the best value 
				for money, but they will equally be looking for the best design, 
				a car that is economical to run – a good cost of ownership – 
				durability and most importantly excellent service. The service 
				factor is a lot more than just when the vehicle needs to be 
				maintained and checked. It has to be the whole customer 
				experience from the moment an individual sets foot in the 
				showroom, to the interaction with the sales staff, when they 
				purchase the vehicle and the follow-up after care they receive. 
				As a business it is our objective to build long-lasting 
				relationships with our customers. Relationships that can last 
				from model to model and from generation to generation. <br>
				<br>
				We are confident that it can succeed – especially in such a 
				positive environment for economic growth and development in the 
				nation. I believe that Oman is on the verge of entering a period 
				of outstanding growth and development that will transform the 
				nation over the next five to 10 years.<br>
				<br>
				Major infrastructure projects that are now starting will be 
				completed, tourism will grow, manufacturing is set to expand and 
				strategic projects such as the Muscat and Salalah Airport 
				projects and Duqm will become operational and act as catalysts 
				for expansion and prosperity. And driven by the deep rooted love 
				in Oman for the motor vehicle in all its forms – I am sure that 
				the Sultanate’s car industry is indeed set to mirror the success 
				of the nation in the years ahead.<br>
				<br>
