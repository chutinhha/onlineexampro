						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						In a league of its own<br>
						</font><font face="Verdana" size="2">
                		<br>
						A mix of unique programming, partnering community based 
						social initiatives and creating innovative solutions for 
						advertisers has helped Al Wisal and Merge FM to 
						establish their clear leadership in the Sultanate’s 
						market<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/media-apr-12-1.jpg" hspace="8" vspace="5" width="300" height="450"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">Radio advertising in Oman 
						has increased from RO1.94mn in 2010 to RO2.17mn in 2011 
						– a jump of close to 12 per cent (Source: IPSOS STAT). 
						This increase is largely attributable to the success of 
						Al Wisal 96.5FM and Merge 104.8FM, the Arabic and 
						English radio stations from SABCO Media. Says Eihab A. 
						Abutaha, CEO Communications, SABCO Group, “Third party 
						statistics shows that we have been growing strongly in 
						the radio market. Al Wisal has established itself as an 
						integral part of the community and this has helped in 
						making it the No.1 radio station in the Sultanate. Merge 
						104.8 since its launch in June 2011, has seen 
						unprecedented growth.”<br>
						<br>
						There are a number of reasons that account for the 
						runaway success of Al Wisal and Merge. The foremost 
						being their programming and content. Says Nadim Attieh, 
						Station Manager, Al Wisal and Merge, “If you listen to 
						the radio stations in Oman, they are either purely 
						entertainment focussed throughout the day or they are 
						formal and serious. We try to strike a balance between 
						these two extremes.” Keeping this in mind Al Wisal and 
						Merge’s programming includes entertainment, sports and 
						weather updates, news, information on the traffic 
						situation, happenings and events around town, social 
						messages, tips on personality development and solutions 
						to everyday problems. Programmes like Chris Fisher in 
						the Morning, the Muscat Drive with Sayeh Stone, and the 
						Lunch Break with Rumaitha, have helped Merge FM, which 
						launched on June 7, 2011, to garner a large market share 
						in a short span of time.<br>
						<br>
						The sequence of programmes through the day reflects the 
						time and mood of the listener. For example, the morning 
						programmes are targeted towards people driving to work 
						and parents dropping their children to schools. The 
						stations give valuable tips on getting better at work, 
						how to raise a millionaire child, negotiating homework 
						better etc. The drive home programmes in the evening are 
						lighter with a lot of music and entertainment, as it is 
						a time when listeners are looking to unwind after a hard 
						day at work.<br>
						<br>
						The programming mix is also attuned to the target 
						audience of these stations. For example, Al Wisal’s 
						primary target group (TG) is Omanis in the age bracket 
						of 16-45 years. The secondary audience being Arab 
						expatriates. In the case of Merge the target audience 
						reflects Oman’s demographic mix and includes English 
						speaking Omanis and, the Asian community, then, 
						Europeans and other expatriates in that order of 
						preference. The station has worked on creating content 
						based on a strong local insight coupled with an 
						international flavour.<br>
						<br>
						Merge has already won multiple awards as recognition of 
						its strong identity and branding. It picked up a top 
						marketing award at the prestigious Gemas Effies Mena 
						Awards 2011. On the eve of its launch, the station got 
						two silver awards at the coveted advertising Lynx Awards 
						2011 (part of the annual Dubai International Advertising 
						Festival), for its unique corporate identity and 
						relevant to its image stationary design.<br>
						<br>
						<b>Community ties<br>
						</b>Al Wisal and Merge FM’s other big differentiation is 
						its close ties with the community. Says Eihab, “At SABCO 
						group, we are clear that our businesses should 
						contribute to the socio-economic development of our 
						nation. This is our vision, and this is what we strive 
						for at all times at SABCO Media. Value and financial 
						success comes as a by-product of how we deliver on our 
						promise. Keeping this in mind our radio stations provide 
						a service that is aimed at educating, informing and 
						entertaining listeners.”<br>
						<br>
						To bridge the gap between the people and the government, 
						Al Wisal started a programme called “Muntada Al Wisal” a 
						year back. This daring but relevant show became an 
						instant success. Today, Muntada Al Wisal is by far, the 
						number one show between the hours of 2pm and 4pm. Today, 
						“Muntada Al Wisal” bridges the gap between all segments 
						of our society, by having an open platform of 
						communication and respect.<br>
						<br>
						Al Wisal and Merge’s social conscience is also reflected 
						in the number of Corporate Social Responsibility (CSR) 
						programmes that they have been involved with. In 2009, 
						Al Wisal did a CSR campaign for kids suffering from 
						cancer, asking people to donate things that would make 
						such children happy. The station put 14 boxes at Nawras 
						stalls across town to collect donated items. The 
						campaign met with an overwhelming response with 
						thousands of boxes with toys, movies, music, books and 
						so on being donated.<br>
						<br>
						In 2011 Merge FM, supported high-profile campaigns like 
						the hugely successful Box Appeal with the Radisson Blu 
						and the Dar Al Atta’a ‘One Rial’ Ramadan campaign. The 
						two radio stations have partnered with campaigns and 
						events of national importance like the Muscat Youth 
						Summit, Safe Driving campaign from the Royal Oman Police 
						etc. Such tie ups engage and add value to organisations 
						that help communities and the needy. Moreover, they also 
						bring these media vehicles closer to the people.<br>
						<br>
						<b>Empirical evidence<br>
						</b>A combination of smart programming, targeting the 
						right audience and being closer to the community has 
						enabled Al Wisal and Merge to emerge as leaders in their 
						respective media categories. This was borne out through 
						an independent survey carried out recently at Sultan 
						Qaboos University (SQU). The survey measures brand 
						equity, perception and loyalty. Students were given a 
						blank sheet of paper and asked to rank various radio 
						stations on these parameters. Al Wisal came out as the 
						unequivocal No.1 in the survey, it had actually got more 
						votes than Hala FM and Shabab Oman put together.<br>
						<br>
						Eihab raises an important point regarding skewed and 
						biased market research that portrays certain media in a 
						negative light. Says Eihab, “We do our own research and 
						track the market. Our research is different from the 
						self adopted research done by certain media houses or 
						publications in Oman. The core competence of these 
						publications is not research. Secondly, how can one 
						media house do research and judge another media. To give 
						an example, a recent survey by a business publication 
						used a sample size consisting of 90 per cent expatriates 
						to pass judgement on radio stations in Oman. This is a 
						glaring example of flawed research and the use of 
						unrepresentative data, as over 75 per cent of the 
						country’s population is made up of Omanis. The chosen 
						sample audience has no relevance to the country’s 
						demographics or even pyschographics and hence such 
						research and surveys have no credibility or relevance.”<br>
						<br>
						The growing popularity of Al Wisal and Merge’s online 
						radio listenership is another indication of their 
						prominence and popularity. Says Nadim, “Most radio 
						stations struggle to get people to listen to them for 
						more than one and a half hours a day, but our online 
						listeners spend close to two hours tuned in to our radio 
						stations. This shows the loyalty that we have created 
						amongst our audience.”<br>
						<br>
						Social media provides another good metric to measure the 
						popularity of radio stations. On Facebook, Al Wisal has 
						3,045 likes, compared to Hala FM’s 378 likes (as of 
						March 21, 2012). Merge has garnered 3,197 likes in nine 
						months, compared to Hi FM’s 5,656 over the last four 
						years – a reflection of the ground that Merge has 
						covered in short span of time.<br>
						<br>
						Says Eihab, “In terms of advertising we are listening to 
						our clients and what they want. We are going through the 
						process of creating awareness on how radio can be used 
						strategically, and this is being appreciated, most of 
						the time. The other big advantage with our stations is 
						that we own our advertising space and have not 
						contracted it to a third party as some of our 
						competitors.” Al Wisal and Merge treat advertising as a 
						part of content helping them to weave ads into their 
						programmes seamlessly, creating more value, for clients. 
						Not content on sitting on its laurels Al Wisal and Merge 
						have drawn up ambitious plans for 2012 and beyond. These 
						include – creating stronger content, being closer to the 
						community, offering creative solutions for advertisers 
						and expanding geographically, locally and regionally. 
						Once executed these initiatives will surely place the 
						two stations leagues ahead.<br>
