						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Doing business with US</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<img border="0" src="images/closeup-apr-12-1.jpg" width="200" height="300" align="right" hspace="13" vspace="3">The 
				two-way trade between the US and all Arab countries stood at 
				$153bn in 2011, up from $119bn in 2010<br>
				<br>
				By Dr Jasim Husain Ali<br>
				<br>
				Bilateral trades between Gulf Cooperation Council (GCC) 
				countries and the US are vital to both sides. The GCC states are 
				a primary source of oil for the US while American products such 
				as aircraft and vehicles help guarantee economic well-being of 
				GCC states. <br>
				<br>
				Undoubtedly, oil prices as well as value of durable American 
				goods play a key role in determining final trade balances. Yet, 
				value of the US dollar is largely insignificant in determining 
				eventual trade activities between the two sides, as all GCC 
				countries except for Kuwait peg their currencies to the dollar. 
				Nevertheless, the American currency constitutes a key component 
				of the basket of currencies used in determining the value of 
				Kuwaiti dinar. <br>
				<br>
				Thanks to steady increases, the six economies of the Gulf 
				Cooperation Council (GCC) have succeeded in reversing the 
				balance to their advantage in 2011 with regard to doing business 
				with the US. <br>
				<br>
				Calculating statistics released by the US Census Bureau, this 
				writer feels that the GCC has collectively enjoyed a $10bn net 
				trade balance in goods in 2011. By comparison, the US recorded a 
				tiny $500mn trade surplus in 2010.<br>
				<br>
				The shortage with the GCC is insignificant by American 
				standards, having sustained a deficit of $737bn in goods trade 
				last year. In fact, the GCC countries accounted for a mere 1.4 
				per cent of total US deficit in goods.<br>
				<br>
				In contrast, China accounted for a hefty $296bn or 40 per cent 
				of the US’s trade deficit in the same year. The deficit would 
				have been worse but for the rise in US exports to the UAE, which 
				grew by a notable $4.2bn to $15.9bn in 2011. The development 
				partly reflects the steady rise in exports of American 
				transportation equipment.<br>
				<br>
				In reality, the UAE stands out among fellow GCC countries by 
				being home to four airlines, namely Emirates, Etihad, Air Arabia 
				and flydubai.<br>
				<br>
				Interestingly enough, the GCC countries were divided equally 
				with regard to having surpluses and deficits in their trade 
				relations with the US in 2011.<br>
				<br>
				At $13.5bn, the US recorded the highest trade surplus with the 
				UAE than with any other GCC state. This is extraordinary as the 
				UAE is a key exporter of crude in the world, yet open for 
				imports.<br>
				<br>
				The size of the UAE’s trade with the US is second only to that 
				of Saudi Arabia in the GCC. In reality, US-Saudi Arabia trade 
				amounted to $62.3bn in 2011, an increase of 42 per cent in a 
				span of a single year. The Saudi side enjoyed a $33.6bn surplus 
				on the back of rising exports by a notable $16bn. <br>
				<br>
				The development chiefly reflects growing exports of crude oil 
				from the kingdom to the US with oil prices remaining steady. 
				More specifically, the increased net imports of petroleum 
				products accounted for about two-thirds of the growth of the US 
				trade shortage in goods last year.<br>
				<br>
				<b>Dependence on imported oil <br>
				</b>This phenomenon serves to prove that the US remains heavily 
				dependent on imported oil and refined petroleum products despite 
				all the talk of efficiency in the use of energy and home-grown 
				options. Not surprisingly, Saudi Arabia plays a key role in 
				trade between the US and the Arab world. As such, trade between 
				the US and Saudi Arabia accounted for 40 per cent of total US 
				trade with all Arab states last year. <br>
				<br>
				The two-way trade between the US and all Arab countries stood at 
				$153bn in 2011, up from $119bn in 2010. Saudi Arabia enjoys 
				numerous qualities such as being the largest Arab economy, 
				boasting GDP of $577bn, as well as serving as the largest oil 
				exporter in the world.<br>
				<br>
				<b>Trade imbalance<br>
				</b>It remains to be seen whether or not the US would press for 
				concluding a Trade and Investment Cooperation Agreement or (TICA) 
				with the GCC bloc if only to offset the trade imbalance. At the 
				moment, the US has separate free trade agreements (FTAs) with 
				Bahrain and Oman. However, the two FTAs were meant to provide 
				the two smallest GCC economies access to the US economy.<br>
				<br>
				Certainly, TICA would serve in enticing investments from wealthy 
				GCC economies currently enjoying windfalls of oil revenues, and 
				the US has to exert efforts for that to happen. <p align="right" class="style13" dir="ltr">
