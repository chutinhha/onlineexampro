				claimed numerous age-group titles around the world including 
				last year’s World Junior crown in San Diego by 11 shots after a 
				nine-under-par 63 on the opening day. He qualified for Volvo 
				China Open in Tianjin after finishing fourth in the mid-China 
				qualifying event in Hangzhou. <br>
				<br>
				“I really couldn’t be happier. I’m so excited right now, it is 
				like a dream come true,” Guan said. “My goal now is to be the 
				youngest player ever to make the cut in a European Tour event 
				and I think if I can shoot level par or better I’ll have a good 
				chance to do that.”<br>
				<br>
				Guan has played in numerous international amateur events and is 
				set to play in the Australian Open and Australian Masters at the 
				end of the year after winning the Aaron Baddeley Invitational in 
				December.<br>
