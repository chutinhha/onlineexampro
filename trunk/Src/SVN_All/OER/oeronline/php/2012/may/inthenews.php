						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Spurring SME growth</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						A new SME Development Fund with a capital of RO100mn 
						will be launched next month in a bid to develop and 
						further promote the small and medium enterprises (SME) 
						segment in Oman </font></b>
						<p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				The SME sector has been found to create approximately 60 per 
				cent of all employment in both high and middle income countries 
				as it uses labour rather than capital as the dominant factor of 
				production. The SME sector provides employment avenues for 
				workers with lower levels of education and skills unlike large 
				corporations who depend on sophisticated technology and 
				automation.<br>
				<br>
				Similarly, large corporations are most often established and 
				provide employment in concentrated pockets near large cities, 
				ports, etc. Despite the presence of mega corporations and large 
				multinationals, SMEs contribute more than 50 per cent of the GDP 
				in developed countries. In Oman, the share of the SME sector is 
				a fairly satisfactory 23 per cent; however, there is 
				considerable potential for further development.<br>
				<br>
				<b>Private sector SME fund <br>
				</b>To achieve this objective, government with the help of 
				Partnership for Development (PFD) partners, an offset 
				initiative, is promoting ‘The SME Development Fund’, capitalised 
				at RO100mn and set up in the private sector. The SME Development 
				Fund would help existing SMEs to grow in scale and scope and 
				also seek to ‘create’ new entrepreneurs and SMEs. The capital 
				for the fund will be raised from pension funds and other 
				investors in Oman and overseas; the PFD partners are arranging 
				low interest loans of €50mn and providing grants (first tranche 
				$30mn) to support the activities of the fund. <br>
				<br>
				<b>The four-point plan<br>
				</b>A survey carried, on behalf of the PFD partners, confirmed 
				the potential for developing the SME sector, identified key 
				actions to be taken by the fund and revealed that such a fund 
				would be viable. Based on the findings of the survey and the 
				prior experience of the CEO, a four point plan has been drawn up 
				to address the needs of the SME sector. Accordingly, the fund 
				would seek to accomplish the following: <br>
				<br>
				Training: This will initially be provided to college and 
				university students and later rolled out to schools and the 
				community in general, to enhance the interest in and 
				understanding of entrepreneurship. This will be in the form of a 
				brief ‘Orientation’ seminar, to generate interest, offered to 
				around 50,000 students annually. A more detailed ‘Development of 
				Entrepreneurship’ course, addressing the shortcomings identified 
				among entrepreneurs, will be offered to candidates selected from 
				the above.<br>
				<br>
				Nurturing: It comprises accounting support, low cost software, 
				monitoring and mentoring, to reinforce the training provided 
				(point i) and to support the post disbursal monitoring carried 
				out by the lenders. The mentors will also assist the SMEs in 
				accessing markets.<br>
				<br>
				Funding: It will be offered speedily and flexibly to address the 
				biggest problem faced by SMEs, the non-availability of 
				appropriate finance. Part of the grant finance is earmarked for 
				subsidising interest charged to eligible SMEs. Creating a 
				conducive environment through interventions with the tender 
				board, large companies, the free trade zones and the industrial 
				estates, to ensure that registered SMEs are at least given 
				favourable consideration when they quote. The fund would 
				consider empanelling SMEs in a transparent manner, to facilitate 
				this exercise.<br>
				<br>
				<b>Structure<br>
				</b>The SME Development Fund has been structured as two 
				entities: a fund registered with the Capital Market Authority 
				and a special purpose vehicle, registered as a company with the 
				ministry of commerce &amp; industry (MOCI). The latter, called the 
				National Company for Projects and Management (NCPM), will 
				receive, manage and disburse the sinking-fund (grant). The fund, 
				presently under formation, will raise the capital and debt and 
				operate the elements of 4 point plan, using third party service 
				providers as depicted below.<br>
				<br>
				The fund will be managed by a board, representing the interests 
				of the investors, and NCPM will be governed by an advisory 
				committee, constituted by MOCI to approve and oversee the 
				utilisation of the sinking fund (Grant).<br>
				<br>
				The fund aims at creating 7,500 SMEs creating 50,000 jobs and 
				will contribute RO100,000,000 to the gross domestic product 
				(GDP) annually at the tenth year of its existence. The fund will 
				visit colleges and schools in Oman for creating an 
				entrepreneurial ambience and has a target to cover 500,000 
				students over a period of 10 years.&#8194;</font></td>
