						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The road ahead in ICT</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<img border="0" src="images/closeup-may-12-1.jpg" width="150" height="225" align="right" hspace="13" vspace="3">Although 
				GCC countries are showing growing appreciation for ICT, they 
				still need to overcome some critical shortages<br>
				<br>
				By Dr Jasim Husain Ali<br>
				<br>
				The six-nation Gulf Cooperation Council (GCC) countries are 
				showing growing appreciation for information and communication 
				technologies (ICT) and for good reasons. This is demonstrated in 
				the recently released The Global Information Technology Report 
				2012.<br>
				<br>
				The World Economic Forum issues the annual report in partnership 
				with the renowned business school, Insead. The latest report, 
				the 11th since its inception, provides ranking for some 142 
				nations, collectively accounting for 98 per cent of gross 
				domestic product (GDP) of the world, or ones that matter the 
				most.<br>
				<br>
				The report ranks reviewed countries on the basis of their 
				achievement on the Network Readiness Index (NRI), in turn 
				derived from performance of ranked countries on 10 pillars 
				generated through four sub-indexes. These sub-indexes comprise 
				1) environment (political and business conditions) 2) readiness 
				(infrastructure, affordability and skills 3) usage (individual, 
				business and government) and 4) impact (economic and social).<br>
				The study is noted for being uniquely comprehensive through 
				combining hard data variables such as number of personal 
				computers and internet penetration rate as well as views 
				expressed by executives. Nevertheless, there is a shortcoming as 
				opinions provided by professionals tend to be subjective.<br>
				<br>
				<b>Bahrain tops <br>
				</b>Surprisingly, Bahrain succeeded in attaining the best 
				ranking among GCC, Arab and Muslim countries. Much to its 
				credit, Bahrain has advanced by three notches, thereby 
				overtaking the UAE and Qatar as well as Malaysia. More 
				specifically, Bahrain secured ranking number 27 globally, ahead 
				of Qatar, Malaysia and the UAE, in turn ranked numbers 28, 29 
				and 30, respectively. The achievement reflects unique qualities 
				of Bahrain including having exceptionally educated locals by 
				regional standards. To be sure, a good number of locals attend 
				evening classes as part of efforts of sustaining their carrier 
				opportunities.<br>
				<br>
				Bahrain’s strengths include financial market sophistication, as 
				evidenced by the financial services sector comprising nearly one 
				quarter of the country’s GDP in constant terms. Conversely, the 
				oil sector emerges as top contributor of GDP in current terms, 
				reflecting steady oil prices.<br>
				<br>
				Ranking numbers for the other GCC countries are 34, 40 and 62 
				for Saudi Arabia, Oman and Kuwait, respectively. Altogether, GCC 
				countries are ahead of many European Union countries when it 
				comes to ICT including Lithuania, Cyprus, Portugal and Spain, to 
				name a few.<br>
				<br>
				<b>Demographics<br>
				</b>Also, GCC countries are ranked ahead of emerging BRICS 
				countries (Brazil, Russia, India, China and South Africa). 
				Certainly, demographics play a factor in all GCC countries, as 
				youths who make up half of the populations, have appreciation 
				for technological materials. Nowadays, many perform routine 
				internet functions including use of email via smartphones. For 
				some, carrying a laptop is an essential part of being 
				dressed-up.<br>
				<br>
				Still, GCC countries need to overcome some critical shortages 
				including limited availability of and attraction of scientists 
				and engineers. As such, there is a serious problem when it comes 
				to innovation.<br>
				<br>
				Needless to say, in the age of globalisation, professionals are 
				in demand and hence can move around, hence the need for making 
				the environment conducive for human capital. Also, the report 
				correctly points out the persistent bureaucratic problem in GCC 
				countries. Bahrain ranks number 132 worldwide when it comes to 
				the number of procedures required to enforce a contract, merely 
				better than 10 countries studied in the report.<br>
				<br>
				The bureaucratic constrain runs contradictory to technological 
				appreciation, all the more pointing out to availability of room 
				for streamlining ranking in future editions of NR.&#8194;</td>
