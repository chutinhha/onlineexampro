				<br>
				Karen Blumenthal is a critically acclaimed children’s 
				non-fiction writer and a long-time journalist for the Wall 
				Street Journal. She is the author of Bootleg: Murder, Moonshine, 
				and the Lawless Years of Prohibition, which received four 
				starred reviews, Six Days in October: The Stock Market Crash of 
				1929, which was a Sibert Honor Book, and Let Me Play: The Story 
				of Title IX, which won a Jane Addams Children’s Book Award. 
				Though Issac Walterson’s authorised biography of Steve Jobs has 
				been getting most of the attention, Blumenthal’s Steve Jobs: The 
				Man Who Thought Different is an equally incisive and perceptible 
				account of this legendary figure. <br>
				<br>
				<b>Down memory lane</b><br>
				From the start, Steve’s path was unpredictable. He was given up 
				for adoption at birth, dropped out of college after one 
				semester, and at the age of twenty, created Apple in his 
				parents’ garage with his friend Steve Wozniack. Then came the 
				core and hallmark of his genius--his exacting moderation for 
				perfection, his counterculture life approach, and his level of 
				taste and style that pushed all boundaries. A devoted husband, 
				father, and Buddhist, he battled cancer for over a decade, 
				became the ultimate CEO and made the world want every product he 
				touched. <br>
				<br>
				Blumenthal takes us to the core of this complicated and 
				legendary man while simultaneously exploring the evolution of 
				computers. Framed by Jobs’ inspirational Stanford commencement 
				speech and illustrated throughout with black and white photos, 
				this is the story of the man who changed our world. Blumenthal 
				weaves her portrait on the thematic frame used by Jobs himself 
				in his autobiographical 2005 Stanford commencement address. She 
				‘connects the dots’ that led him from his adoption as an infant 
				through his ‘phone phreaking’ days to a spectacular rise and 
				just as meteoric fall from corporate grace in the 1980s.<br>
				<br>
				Following a decade of diminished fortunes and largely 
				self-inflicted complications in personal relationships, he 
				returned to Apple for a spectacular second act that also turned 
				out to be his final one. Despite getting bogged down 
				occasionally in detail, the author tells a cohesive tale, 
				infused with dry wit (‘He also considered going into politics, 
				but he had never actually voted, which would have been a 
				drawback’). The book takes one through the roller coaster ride 
				life of Steve Jobs, the genius, maverick, rebel and visionary. 
				The book is thoroughly researched and clear on the subject’s 
				foibles as well as his genius. A perceptive, well-wrought 
				picture of an iconic figure well worth admiring – from a 
				distance.&#8194;<br>
