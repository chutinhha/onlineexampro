						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Agility is the key</strong></span><p class="style15">
				<span class="style14"><strong>Dr Amer Awadh Al Rawas, CEO, Oman 
				Telecommunications Company underscores the reasons for the 
				company’s enduring success on the OER Top 20 charts in a candid 
				chat with Mayank Singh and Visvas Paul D Karra</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-may-12-1.jpg" style="border: 1px solid #808080; " width="500" height="235"></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<br>
				<b>Omantel has been ranked as the No. 1 company on the OER Top 
				20 list since it listed on the MSM in 2005. What has enabled the 
				company to consistently hold its ground? <br>
				</b>We have been able to transform and improve our competitive 
				readiness. We have taken a number of initiatives in this 
				direction – one, we have improved our internal efficiency by 
				merging the two companies (Omantel and Omanmobile) under one 
				operating model and finally into one company. Second, we have 
				transformed ourselves into a customer oriented company, offering 
				products and services according to their needs, desires and 
				aspirations and delivering those with an improved customer 
				experience. The third dimension is improving our infrastructure 
				to increase the quality and reliability of our service. Omantel 
				has achieved an average of nine per cent annual growth since 
				competition started. <br>
				<br>
				<b>Being the biggest may not necessarily be synonymous with the 
				best. How do you ensure that size does not become a deterrent to 
				efficiency? <br>
				</b>If you look world over a lot of big companies have lost 
				ground to newer companies. Size is not enough to survive, 
				survival is all about agility. Agility is the ability of an 
				organisation to transform itself when the environment around it 
				changes. Companies which are not able to move from an incumbent 
				status lose ground. The transformation that Omantel has 
				undergone since 2009 underscores our ability to transform 
				ourselves and compete in the market. All our business domains 
				are open to competition, but despite this we are holding ground 
				and doing well in the market.<br>
				<br>
				Internal efficiency in the telecom sector is judged by EBITDA, 
				which is the money that you make from your revenue. Omantel’s 
				EBITDA of 53 per cent (2011 figures) is the highest in the 
				region amongst telecom players. This is commendable as our 
				operations are diverse due to our size in the market. In the 
				fixed line service for example, we have 222 fixed line exchanges 
				and somebody has to manage them and this requires a larger staff 
				strength. Omantel has three times the staff compared to our 
				nearest competitor. We also have a larger asset base with 
				commensurate depreciation, but it is good for our shareholders.<br>
				<br>
				<b>Can you cite a few measures taken by Omantel that reflect its 
				nimble footedness?<br>
				</b>Muscat has been designed according to an urban plan that 
				dates back to around 40 years. As the city landscape has 
				changed, it has become increasingly costly and difficult to 
				provide services according to our previous plans. Imagine the 
				cost of digging up trenches, laying cables and providing the 
				last mile services in a changed landscape.<br>
				<br>
				Instead we brought in an innovative solution that acts as a 
				neighborhood exchange. For example, switches were built 
				according to an older urban plan, so accordingly the switch at 
				Madinat as Sultan Qaboos (MQ) served Al Khuwair as well. As Al 
				Khuwair expanded, the switch at the Al Khuwair roundabout had to 
				serve Al Khuwair 33 and the Bausher area. As the landscape 
				changed, we could have expressed our helplessness, but instead 
				we installed new switches and have grown our broadband service 
				too. Omantel used to charge customers with deposits, security 
				etc, but all those charges have now been wiped away. We moved 
				into unlimited offers, such offers require one to adjust various 
				systems so that one can handle the increase in traffic. We were 
				able to open up to the idea of MVNOs and resellers, adapt and 
				use them in a positive way to complement our work in the market.<br>
				<br>
				<b>Omantel is offering all-time low rates for its services – one 
				can avail of a 1GB monthly Hayyak broadband connection for RO5, 
				off-peak rates for international calls to India have dropped to 
				RO0.065 per minute. What has enabled you to drop rates so 
				drastically, does it also mean that the company was overcharging 
				its customers earlier? <br>
				</b>This is linked to what I said earlier; you need to build 
				your capacity to render a service, and before you are able to 
				give it at a lower price or as an unlimited offer. It all comes 
				down to infrastructure and once you reach a certain scale then 
				you can offer unmatched innovative offers and prices. As a 
				company we need to strike a balance because if we invest too 
				much in infrastructure then the depreciation will be too high 
				and the company will not be in a position to give shareholders 
				the return on investments that they expect. So there has to be a 
				tight balance between these competing objectives of pleasing 
				customers, building infrastructure and offering shareholder 
				value.<br>
				<br>
				<b>What is Omantel’s broadband penetration rate and what kind of 
				a percentage growth do you expect this year? </b><br>
				We have grown our fixed broadband (ADSL), not just in terms of 
				the number of subscribers but also in the type of subscribers 
				that we are getting. Earlier people struggled to get half MB for 
				RO12, but now every subscriber gets two MB. We have customers 
				who have taken upto 40 MB, but for most people we can offer upto 
				24 MB, if they do not have fibre. Not only have the number of 
				subscribers grown but so has the level of service and that is 
				helping our revenues to grow. The number of subscribers for our 
				mobile broadband service too has grown tremendously, although we 
				are very conservative in calculating our subscriber base. We 
				calculate our subscriber base by measuring whether a customer 
				has been using his line for “certain period” at least for one 
				month. So if customers have not been using their mobile phones, 
				then we take them off our records and such prudence has worked 
				in our favour. Our mobile broadband subscriber base stood at 276 
				K as of December 31, 2011.<br>
				<br>
				<b>Oman’s telecom market has a mobile penetration of almost 170 
				per cent, given such a scenario where is Omantel’s future growth 
				going to come from? Is there also a fear that your average 
				revenue per user (ARPUs) may come down drastically from here 
				onwards?</b><br>
				We have been in a position to sustain our ARPUs for a long time 
				now. Worldwide, ARPUs have been declining for telecom companies 
				as rates are going down and we cannot fight this trend. However, 
				we can counter this by allowing and encouraging customers to use 
				more devices. So we can increase revenues either through 
				enhanced machine-to-machine communication, usage of additional 
				devices and services like broadband.<br>
				<br>
				<b>A lot of people say that mobile service pricing is still 
				higher in Oman compared to other markets like India?</b> <br>
				It is unjustified to compare Oman to India because of the 
				difference in the scales of the two countries. In the Arab 
				world, our rates are one of the best and this has been proved by 
				reports from independent bodies like Arab Advisors Group and 
				Arab Regulators Network. Though the size of the market is a 
				challenge we will continue to focus and build our infrastructure 
				for three reasons – one, we want to partner and contribute to 
				economic growth by building telecom infrastructure in newly 
				developed economic zones; second, to enable the business of our 
				corporate customers to grow and finally, better coverage to 
				satisfy our consumers and as a contribution to the community.<br>
				<br>
				<b>Being a partially owned government company, does Omantel have 
				to live up to a larger social obligation and is this a burden 
				from a business point of view?</b><br>
				We are fulfilling our social obligations by continuously 
				covering places that are not necessarily profitable. We do this 
				for two reasons, to satisfy and please our customers and to 
				fulfill our corporate social responsibility and we will continue 
				to do this as a contribution to the national economy.<br>
				<br>
				<b>After WorldCall’s acquisition in 2008 Omantel has not made 
				any forays in the international market. Is this a sign that you 
				are going to be focused on Oman in the near future?<br>
				</b>The GCC region has not witnessed a significant number of 
				mergers and acquisitions in the last three years as there have 
				been a few compelling opportunities. The Greenfield 
				opportunities that came up were not appropriate for our size. We 
				are constantly on the lookout for M&amp;A opportunities that are in 
				conformity with our size and offer a reasonable return on 
				investment. We have a set of criteria to filter proposals, but 
				until now we have not found any good opportunity. We are happy 
				with WorldCall’s last year performance as it ended 2011 with a 
				profit of $3.4mn.<br>
				<br>
				<b>What is the company’s growth strategy for 2012 and are there 
				any specific areas of operations that you are targeting?<br>
				</b>Enhancing our broadband service is a specific growth area 
				that we are targeting. So we will upscale our broadband 
				infrastructure. In addition, we are looking at building 
				corporate solutions and increasing our international capacity, 
				connectivity and service.&#8194;</td>
