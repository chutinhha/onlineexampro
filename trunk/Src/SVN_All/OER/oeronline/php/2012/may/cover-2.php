				and its affiliates cater to around 35 per cent of the total 
				power capacity of the Oman Main Interconnected Systems (MIS) and 
				20 per cent of the water capacity for the main supply zone. The 
				business model of both the project companies held by SMN Power 
				Holding is based on a strong contractual framework, with solid 
				and reliable partners. Back-to-back contracts significantly 
				reduce the risks over a long-tern period. The supply of the 
				output to the off-taker Oman Power and Water Procurement Company 
