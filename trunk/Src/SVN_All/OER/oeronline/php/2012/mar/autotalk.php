						<font face="Verdana" size="4" color="#C00000">An 
						Unmistakable Presence</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-mar-12-1.jpg" width="510" height="339" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				The Chrysler 300C offers great styling, surprisingly agile 
				handling and plenty of legroom. An OER report<br>
				<br>
				When the original Chrysler 300 debuted in 2005 read the modern 
				original, as the nameplate first appeared in 1955), it was a 
				dramatic affirmation that the classic American sedan – a big, 
				rear-wheel-drive, V-8 – could still be relevant. With its 
				blocky, vaguely Bentley-esque design, the car was an immediate 
				hit. It was a darling of today’s tastemakers, the hip-hop crowd, 
				and was frequently customised. Much was made of the fact that 
				Snoop Dogg phoned up then-Chrysler president Dieter Zetsche 
				looking to get one. In the following years, the fervour over the 
				300’s styling cooled, and people began to take a more critical 
				look at the car underneath. Given Chrysler’s tumultuous years 
				and new ownership under Fiat, it’s safe to say that expectations 
				for the revamped 300 were not high. The new 300, however proves 
				to be one of the biggest surprises in recent automobile history. 
				It’s a sterling example of Chrysler’s technical and engineering 
				prowess. <br>
				<br>
				<b>Turning a new leaf</b><br>
				The reworked sheet metal appears at first to be just a tweaking, 
				but it actually incorporates significant changes, such as a less 
				upright windshield and massive pillars, both of which aid 
				visibility. The car also offers a lot more features like LED 
				daytime running lights, LED tail lamps, and a more chrome-heavy 
				visage. The more evident changes are inside. The visuals are 
				brighter and glitzier, starting with a standard 8.4-inch 
				touch-screen that incorporates a variety of controls, although 
				there are large knobs and some physical buttons for important 
				HVAC and audio functions. The other good news is that the 
				screen’s touch points are large and the logic is clear. Gauges 
				with thick chrome surrounds and bright blue LED lighting (day or 
				night) also makes quite a visual impression, and real wood trim 
				is liberally used, including on the thick rim of the steering 
				wheel. The oversized seats are comfortable, the optional leather 
				is high quality, and soft-touch surfaces abound. <br>
				<br>
				The list of standard and available features is long. Options 
				include adaptive HID headlamps, blind-spot warning and rear 
				cross-path detection, adaptive cruise control with forward 
				collision warning, and a panoramic sunroof. Standard items (on 
				the 300C) include heated and ventilated seats, heated rear 
				seats, heated steering wheel, power steering column, heated and 
				cooled cup holders, Nappa leather, keyless ignition and remote 
				start, navigation, a backup camera, and LED ambient interior 
				lighting. <br>
				<br>
				<b>More than skin deep</b><br>
				The reworking of the 300, however, goes deeper than upgrading 
				the interior and adding a lot more equipment. There’s been some 
				reengineering of the car underneath, and that’s noticeable too, 
				once you drive it. There’s a new electric power steering system, 
				and it’s tuned well enough – precise and well weighted – that it 
				easily could pass for hydraulic. The suspension has been 
				revamped, and on the 300C AWD, which has the Touring-tuned 
				Suspension (larger anti-roll bars and firmer dampers), it felt 
				pleasantly tied-down but still delivered an exceptionally 
				comfortable ride – particularly given the standard 19-inch 
				wheels. Engineers also threw a bunch of noise-reduction measures 
				at the car, and indeed it is a quiet cruiser.<br>
				<br>
				The V-8 powertrain essentially carries over, and the 
				much-vaunted Hemi and the five-speed automatic turn out to be 
				one of the strongest aspects of the new car. The Hemi is sweet 
				sounding and powerful. The optional all-wheel-drive system 
				provides the exhilaration of a sports car. As an example of a 
				true, American-style luxury car, the 300 pretty much stands 
				alone. The 300 has it all – a powerful V-8, rear-wheel drive, a 
				roomy interior, plenty of flash, lots of gadgetry, and solid 
				street presence. The most surprising thing about the 300 is that 
				all the chrome and leather, all the electronics and LEDs, do not 
				feel like a thin veneer of glitz papering over a cheap 
				substrate. The car is quiet, the powertrain is smooth, the 
				steering is direct, the seats are firm, and the ride quality is 
				good.&#8194;<br>
