						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Tapping the alumni 
						potential</strong></span><p class="style15">
				<span class="style14"><strong>Prof Atanu Ghosh, Dean-Alumni &amp; 
				External Relations, Indian Institute of Management-Ahmedabad (IIM-A), 
				one of India’s premium business schools, was recently in Muscat 
				to reach out to the alumni of the 50-year old institute in the 
				city. Muhammed Nafie catches up with him for a freewheeling 
				interview. Excerpts:<br>
&nbsp;</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face-mar-12-1.jpg" style="border: 1px solid #808080; "></font><p align="justify" class="style13">
				<br>
				<b>What is the specific purpose of your visit to Oman?<br>
				</b>We are looking to revive our alumni relations and activities 
				at various chapters. Today we have got 17 alumni chapters in 
				several countries including the US, the UK, Singapore, Oman, and 
				the UAE. We had a year-long grand celebration of our golden 
				jubilee which concluded in December 2011. And it was the 
				occasion when we decided to reach out to the alumni in order to 
				interact with them, respond to their queries and make them 
				contribute both intellectually and finically for their alma 
				mater. We have already visited the alumni chapters in Singapore 
				and the US (both in the west coast and east coast) and now we 
				are doing it in Muscat and Dubai. In India we did it in Delhi, 
				Calcutta, Bangalore and Hyderabad. We also show them a film on 
				IIM-A created on the occasion of its 50th anniversary.<br>
				<br>
				<b>How are you going to utilise the skills and expertise of your 
				alumni for the institute?<br>
				</b>IIIM-A has got a large number of its former students working 
				as CEOs all over the world. According to a recent survey by a 
				business magazine, a whopping 62 per cent of CEOs of 
				professionally managed companies in India are from IIM-A. The 
				alumni can play an active role in the progress of the institute 
				in multiple ways. Those who are keen to teach can spend some 
				hours with us to take guest lectures and those who are 
				academically inclined can take even full courses. In addition, 
				as most of them are managing companies, they can assist in 
				preparing various case methods which the IIM-A employs to 
				prepare its students for the challenges of management and 
				leadership. As we have started with the full support of Harvard 
				business school, all our initial faculty members were sent to 
				Harvard to get used to its case methods which we have been suing 
				since last 50 years. These days the cases get obsolete very soon 
				because the environment is changing and information is flowing 
				so fast so that we need to constantly recharge and renew the 
				cases and get new cases. <br>
				<br>
				<b>IIM-A have signed MoUs for academic cooperation with various 
				international business schools. IIM-Indore is planning to join 
				other IIMs in offering prestigious management programmes in the 
				Gulf. Do you have any plan to foray into the region?<br>
				</b>In 2010, the Government of India permitted IITs and IIMs to 
				set up branches in all cities in India and campuses outside the 
				country, and since then they have been proactively seeking 
				opportunities outside India. Till 2010 we were not able to set 
				up our own campuses abroad. However, we had signed MoUs with 70 
				business schools internationally for academic tie-up. As per 
				these MoUs, we exchange students for three months with various 
				schools in the US, Europe, Australia and China. This has become 
				very prominent in our campus calendar.<br>
				<br>
				In addition, IIM-A has a double-degree programme agreement with 
				ESSEC, France and University of Bocconi, Italy. As per this 
				agreement, our second year students will spend a term in France 
				or Itlay while the foreign students are permitted to complete 
				their courses at IIM-A, and they get degrees from both the 
				institutes.<br>
				<br>
				Another way of cooperation is to send out our PhD students 
				abroad so that they complete the course partly from here and 
				partly from there and get their Fellow Programme in Management 
				(FPM), which is equivalent to PhD, from both the schools. Apart 
				from the exchange of students, we also do exchange of faculties. 
				We are facing a faculty crunch. Since 1982 when I passed out 
				from IIM-A, the number of students have grown up 150 per cent 
				while the number of the faculty has seen a marginal increase of 
				below 15 per cent. That is also why we are not venturing out for 
				foreign campuses.<br>
				<br>
				However, this does not mean that we don’t have plan to open 
				shops in the region. We would certainly look at the Gulf region 
				to set up an IIM campus in the future.<br>
				<br>
				<b>How important is research at IIM-A? Are IIMs and IITs at a 
				level playing field vis-a-vis their counterparts in the West, 
				when it comes to research? <br>
				</b>IIM-A has made its mark on its Post Graduate Programme (PGP), 
				which is equivalent to MBA, and on our students’ placement as 
				reflected in the successful careers of our alumni which I have 
				already mentioned. What the research meant for us is still a 
				grey area. We consider case research as an important area of 
				research. We try to capture something happening in an 
				organisation or industry and try to build these cases by taking 
				them to class rooms for discussion and analyses. IIM-A has 
				always on the lead for the case research and case studies. We 
				are able to make an impact, when we try to study what is going 
				on in an industry or an organisation. But when it comes to 
				academic and theoretic research the western world is talking 
				about, we are a little bit lacking. Our faculties are driven by 
				their own choices. We don’t really want to push someone to one 
				corner. Some are absolutely interested in research while some 
				are interested in doing consulting with organisations whereas 
				some others are excellent teachers. However, we think that now 
				the time has come for creating some sort of balance in it.<br>
				<br>
				However, IITs are doing some researches which are almost cutting 
				edge and comparable to anywhere in the world. But in terms of 
				number that would not be on a par with a research driven 
				institute in the West. It depends on a lot of other factors such 
				as the number of people available, facilities and funding. The 
				kind of support and funding from government and other 
				organisation in the western world is much larger than institutes 
				in India get.<br>
				<br>
				<b>It is said that failures in management education are partly 
				to blame for the global economic crisis. How does management 
				education require evolving after the crisis? <br>
				</b>Business education is at a major crossroads. Though MBA 
				graduates from top-tier schools set the standard for 
				cutting-edge business knowledge and skills for decades, now the 
				business world has changed. Recently professor Srikant M Datar, 
				an IIM-A alumnus who was the Arthur Lowes Dickinson Professor at 
				Harvard University for 10 years, coauthored a book titled 
				‘Rethinking the MBA: Management Education at a crossroads’. The 
				book, questioning the conventional business education, examines 
				the importance of synthesizing ‘knowing’, ‘doing’ and ‘being’ in 
				education. The book says knowing or acquiring knowledge which 
				happens in class room must be coupled with its practical 
				application. Even while doing the MBA, the students should go to 
				the organisations to put the knowledge learned into practice. 
				The third one, being, is related to realising what one wants to 
				be and what should be the purpose of one’s life.<br>
				<br>
				Recently professor Srikant did four workshops for IIM-A and IIM-Calcutta 
				together to spread this message to other business schools. In 
				IIM- A, we have reviewed our programmes and introducing some of 
				these elements. Similarly we have summer internship where 
				students are supposed to go and apply their theoretical 
				knowledge into practice. We are also trying to introduce courses 
				such as business ethics as part of our programme. MBA programmes 
				need to give the students the heightened cultural awareness and 
				global perspectives they need.&#8194;<br>
