				<b>
				<font face="Verdana" size="4" color="#C00000">TOURISM ENRICHES</font></b><p align="justify" class="style13">
				In regards to tourism, Oman’s Vision 2020 dovetails five areas 
				of focus – establishing Oman as a regional travel and tourism 
				hub; increasing the contribution of the industry to GDP to not 
				less than three per cent; increasing the participation of the 
				private sector and activating its role in the development of 
				various tourism activities; attracting foreign direct investment 
				(FDI) and increasing the ‘Omanisation’ percentage of the 
				industry to 80 per cent.<br>
				<br>
				The Ministry of Tourism has set its sights on attracting 12 
				million visitors to Oman by the year 2020, and based on forward 
				visitor arrival projections the ministry is confident that the 
				tourism sector will become one of the biggest contributors to 
				Oman’s GDP by 2020. <br>
				<br>
				The ministry’s aspirations have been supported by the government 
				and private sector with investments of over $10bn in several 
				major tourism projects across the Sultanate. <br>
				<br>
				With its scenic beauty and hospitable culture, the Sultanate 
				lends itself to tourism naturally. Various surveys and studies 
				underscore the strength of Oman as a destination of choice. <br>
				<br>
				The country has been ranked among the ‘20 Top Tourist 
				Destinations in the World’ by the National eographic’ magazine. 
				Muscat has been ranked as the second best city in the world 
				after London to visit in 2012 by BBC’s Lonely Planet, the 
				popular travel guide publisher affiliated with the British 
				Broadcasting Corporation. Muscat city has also been declared the 
				Arab Tourism Capital for 2012 by the Arab Tourism Ministers’ 
				Council. <br>
				<br>
				Tourism promotion requires a multipronged approach like building 
				the right infrastructure, creating ready connectivity and 
				offering alluring packages. Meetings, Incentives, Conferences, 
				and Exhibitions or MICE is a good way of luring business 
				tourists to the Sultanate. Experience shows that business 
				travellers are likely to visit a MICE destination again on 
				vacation. <br>
				<br>
				Visvas Paul D Karra’s cover story in this issue takes an 
				in-depth look at this tourism segment. It’s the first time that 
				any publication in the Sultanate has done such a profound 
				analysis of this significant trend.<br>
				<br>
