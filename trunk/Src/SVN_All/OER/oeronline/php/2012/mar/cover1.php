						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						MICE is right for Oman</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				Apart from government delegations, a large number of 
				international associations conduct meetings in various parts of 
				the world for their members. And Oman has been host to a number 
				of such conferences. The cardiology conference mentioned in the 
				beginning of this article is one of such instances. In fact, the 
				medical sciences associations have an 18 per cent share of the 
				MICE segment globally (See Table 3 for complete list) and it 
				occupies the first spot in the Top 10 key industry International 
				Associations. <br>
				<br>
				HE Khalil says Oman offers all amenities and facilities that a 
				MICE traveller requires. If you look at the criteria for MICE 
				requirements, then Oman fits the bill perfectly, as the country 
				has quality accommodation; international accessibility (ie 
				international airport and airline serviceability); calibre of 
				local bidding hosts; reputation to host successful events; 
				proactive approach and support; strength in specific field or 
				industry; networking, education and business opportunities and 
				compatibility for English speaking delegates.<br>
				<br>
				<b>OCEC jewel</b><br>
				International associations look for world-class venue with large 
				capacities to conduct their meetings and conventions. Although 
				Oman lacks such a facility currently, the upcoming Oman 
				Convention and Exhibition Centre (OCEC) will become the jewel of 
				the MICE segment in Oman’s tourism map. The centre being 
				developed by the state-owned Omran will enable Oman to bid for 
				regional and international events that require seating capacity 
				of between 1,500 and 3,000.<br>
				<br>
				This will add to Oman’s current tourism offer and generate 
				business for other properties through break-out and out of 
				session meetings thus attracting business and leisure activity 
				to Oman that would not otherwise happen.<br>
				<br>
				One of the important developments for Oman’s MICE segment is the 
				appointment of AEG Ogden, one of the world’s leading convention 
				and exhibition management companies to operate OCEC. Rochelle 
				Uechtritz, group manager, convention sales development of AEG 
				Ogden says that her company is both proud and honoured to have 
				been entrusted with the responsibility of managing what will 
				become an iconic venue in the Gulf region.<br>
				<br>
				AEG Ogden had been involved in the design and planning of the 
				OCEC to date and would now utilise its experience, extensive 
				worldwide network and expertise in preparing the centre for 
				opening and competing for its share of the lucrative business 
				events market. <br>
				<br>
				She says that there are more than 200 convention and exhibition 
				centres competing on the world stage for a slice of the business 
				events pie but research already undertaken has identified 
				international conventions with the potential to be held in Oman 
				as far ahead as 2024. Continuing further, she says, as 
				international conference organisers look for venues well in 
				advance for their future events, we as the operator of the OCEC 
				have already begun marketing the venue on a global scale.<br>
				<br>
				Omran states that OCEC’s exhibition hall and the car parking 
				facility, which are in the first phase, will be ready by 
				mid-2014. Then the auditorium, banquet halls and meeting rooms 
				will be ready by late 2015. AEG Ogden is aiming at three market 
				segments – conventions, exhibitions and one-day meetings and 
				seminars – for business once it starts operations.<br>
				<br>
				<b>Positive signs</b><br>
				Meanwhile, HE Maitha is of the opinion that exhibitions 
				conducted at the Oman International Exhibition Centre (OIEC) 
				along with events like the Muscat Festival and the Tour of Oman 
				(cycling event) have multiple synergies and draw hundreds of 
				people from around the world to Oman generating a lot of 
				economic value for the country.<br>
				<br>
				She says that in order to promote Oman as the MICE destination, 
				the ministry is putting together a complete calendar for the 
				next three to five years. “Further, our partners like the 
				hoteliers are promoting Oman not only for leisure but also for 
				MICE. We have quite a few hotels which already have 
				state-of-the-art facilities for holding conferences and events. 
				Let’s admit that the hotels are promoting Oman as a MICE 
				destination (see Table 1 for list of hotels).”<br>
				<br>
				Shangri-La’s Barr Al Jissah Resort &amp; Spa – one of the five star 
				hotels in Muscat – hosts five to six international events 
				annually during peak season. Add to that two to three major 
				local events every month during the high season. In summer, 
				there is less interest from the international market but the 
				hotel gets two to three local events per month.<br>
				<br>
				According to Peter Donlevy, director of sales and marketing, 
				Barr al Jissah, “the Sultanate has much to offer to the 
				corporate meetings and incentive market as the hotel provides a 
				five star luxury, influenced by the rich cultural heritage of 
				Oman, creating a unique Arabian experience.” The resort boasts 
				of the largest room inventory in Oman with the biggest number of 
				meeting and banquet space. It also has an extensive food, 
				beverage and recreation facility to handle the incentive 
				business.<br>
				<br>
				<b>Upbeat mood</b><br>
				Ahmed Saleh Baabood, executive chairman of Oman International 
				Trade and Exhibitions (OITE) who has been involved with the MICE 
				industry for the past 15 years, is very excited about the recent 
				developments in this segment. The expansion of the Muscat 
				International Airport and the increase in direct flights to 
				Muscat by national carrier Oman Air are all going to contribute 
				positively to the growth of the MICE segment in Oman, says 
				Baabood.<br>
				<br>
				OITE is among the oldest conference and trade exhibition 
				companies in the country and currently hosts at least 25 
				conferences, exhibitions and trade exhibitions annually. Many of 
				its events are held at the OIEC located just one kilometre from 
				Muscat International Airport. Incidentally, the OIEC was 
				established in 1985 and has since been the main venue in the 
				Sultanate for all major exhibitions, fairs and conferences. In 
				total, the facility boasts 12,000 square metres of 
				air-conditioned indoor exhibition space and 3,000 square metres 
				of outdoor exhibition space.<br>
				<br>
				OIEC is home to a unique customs clearance facility, whereby 
				equipment and documents flown in for any event are assured 
				prompt and speedy clearance from the immigration and customs 
				authorities; complemented by an on-site ROP office.<br>
				<br>
				Ahmed, who is the board member of OCCI and head of its 
				investment promotion committee in-charge of MICE says, “Earlier 
				we used to face constraints to get exhibitors because of the 
				connecting flights and cumbersome visa regulations. But all that 
				has changed now. Besides the new airport and more direct 
				flights, we are also seeing a huge jump in the transport 
				infrastructure like the announcement of the expressway upto 
				Dubai border and the railway project. All this will have a 
				positive impact on the industry.” The Ministry of Commerce and 
				Industry has opened a new cell for exhibitions underscoring the 
				seriousness of the government to enhance the MICE segment in 
				Oman, informs Ahmed.<br>
				<br>
				<b>Travel industry</b><br>
				The spurt in tourism-related activity also has a ripple effect 
				on the travel industry, a key player for MICE. “It is a huge 
				industry as people are always looking for new destinations to 
				conduct such shows,” says Shankar Bose, general manager, Bahwan 
				Travel Agencies.<br>
				<br>
				“In the hard world of business, incentives are proving to be a 
				great motivator to spur on performances of executives. For such 
				events, large multinationals do not cut any corners but roll out 
				the best in the hope of retaining loyalty and talent,” he adds.<br>
				<br>
				Bahwan Tours and Bahwan Holidays which are the inbound and 
				outbound tour operations respectively of Bahwan Travels handle a 
				combined capacity of over a 100,000 tourists annually. With the 
				Oman Air Holidays JV, they are looking at increasing the numbers 
				significantly.<br>
				<br>
				Bose feels that on a more grander scale, we need iconic 
				structures like the Opera House and something that is unique to 
				Oman, it’s history and the past. The Ministry of Tourism and The 
				Muscat Municipality are doing a great job towards that end and 
				as time flies, you will see Oman develop into a great tourism 
				destination. And along with this development, economic 
				opportunities will continue to abound, he says.<br>
				<br>
				“We will need more commercial spaces like the beautiful Sultan 
				Qaboos University Auditorium which can accommodate up to roughly 
				5000 people in theatre style if we are to host mega events. 
				Besides all this, we can use nature as the background but we 
				need to paint a larger canvas and ensure that Oman is on the map 
				all the year round by promoting Jebel Akhdar, Jebel Shams and 
				Salalah as the summer destinations,” Bose opines.<br>
				<br>
				“I think we are coming to a point where Oman’s tourism is taking 
				off in a significant manner,” concludes Bose.<br>
				<br>
				We agree with Bose because MICE will give a big boost to tourism 
				in the Sultanate.&#8194;<br>
