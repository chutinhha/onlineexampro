						<span class="style27"><strong>Oman Forum on ‘Building 
						National Talent’</strong></span><p class="style15">
				<strong><span class="style14"><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event-mar-12-1.jpg" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>The Expansion of educational infrastructure, skills 
						development through training and creation of employment 
						opportunities have been the cornerstone of the 
						Sultanate’s impressive march on the path of progress 
						under the dynamic leadership of His Majesty Sultan 
						Qaboos bin Said. Apart from the strategic initiatives of 
						the Government, the private sector has played a pivotal 
						role in the development of national talent.<br>
						<br>
						However, among the key issues facing the Sultanate of 
						Oman, the top challenge is to further build the human 
						talent to meet future strategic goals and lead the 
						nation on the socio-economic development track. OMAN 
						FORUM – BUILDING NATIONAL TALENT, an initiative of 
						Oman’s premier business magazine Alam al-Iktisaad Wal 
						A’mal (AIWA), will bring together the government, 
						private and public sector, workers’ community, academia, 
						consultants and media on a single platform to debate on 
						the state of education and training, youth employment 
						policies and initiatives to identify talent gaps and 
						challenges and to create the guidelines to set up the 
						road map for future.<br>
						<br>
						To be organised in April this year under the patronage 
						of the Ministry of Manpower and in association with 
						Petroleum Development Oman (PDO), OMAN FORUM – BUILDING 
						NATIONAL TALENT has been supported by the Ministry of 
						Higher Education, Ministry of Education, Ministry of 
						Civil Services, Sultan Qaboos University and The 
						Research Council (TRC). AhliBank, Voltamp and Omran are 
						the strategic partners for the prestigious forum. KPMG 
						is the knowledge partner whereas Times of Oman and Al 
						Shabiba are the media partners for the event.<br>
						<br>
						The primary objective of the forum is to promote thought 
						leadership by disseminating information on the subject 
						and constructing innovative ideas for a wider 
						participation of the Omani youth in the national 
						building process and upliftment of the society.<br>
						<br>
						For more information on OMAN FORUM – BUILDING NATIONAL 
						TALENT, contact Ahmed on 99356490.<br>
&nbsp;<p align="justify" class="style13">
				<font size="2" color="#C00000"><b>AIWA Awards for Best 
				Performing Companies</b></font><p align="justify" class="style13">
						<br>
				<img border="0" src="images/event-mar-12-2.jpg" style="border: 1px solid #C0C0C0" width="284" height="457" align="right" hspace="12" vspace="3">AIWA Awards are back. Come May, Oman will witness the 
				most high profile event of its kind targeted primarily at the 
				top echelons of the Arabic speaking business community in the 
				Sultanate. Setting up new benchmarks in the industry, corporates 
				in Oman will be felicitated based on a highly qualitative 
				performance survey done in a transparent manner as per the best 
				international standards. To recognise the top achievers in 
				Oman’s corporate world, Alam al-Iktisaad Wal A’mal (AIWA) has 
				instituted Awards for Oman’s Best Performing Companies. Alam al-Iktisaad 
				Wal A’mal (AIWA), published by United Media Services, is Oman’s 
				leading Arabic monthly business magazine. This year marks the 
				second edition of the AIWA Awards after the highly successful 
				launch in 2011.<br>
				<br>
				In a red carpet awards evening, AIWA will felicitate the top 
				performing listed companies in Oman during 2011. A networker’s 
				dream, the event will be attended by the who’s who of Muscat 
				corporate high life. The premium event will attract more than 
				250 top business leaders, CEOs and senior government officials 
				from a cross-section of industries, ministries and government 
				bodies. It will witness the congregation of the key 
				decision-makers who are driving the country’s business and 
				economy. A high-voltage show by renowned international troupes 
				will make the attendees cherish the evening for a long time to 
				come.<br>
				<br>
				The event will be organized under the patronage of Muscat 
				Securities Market (MSM). Gulf Baader Capital Markets SAOC (GBCM) 
				is the knowledge partner whereas KPMG is the audit partner for 
				the ranking process used for the awards. Overall 15 top ranking 
				companies will receive the coveted trophy. In addition, AIWA 
				will also felicitate two prominent Omani nationals who have done 
				the nation proud by excelling in the fields of business and 
				economy. The AIWA Awards to the selected personalities will be 
				made in two categories – AIWA Global Omani of the Year Award and 
				AIWA Life Time Achievement Award. In addition, AIWA will also 
				felicitate two companies for their immense contribution to the 
				society and economy. They will be presented with the trophies 
				for ‘Excellence in Corporate Leadership’.<br>
				<br>
				For more details on the AIWA Awards, contact Ahmed on 99356490.
