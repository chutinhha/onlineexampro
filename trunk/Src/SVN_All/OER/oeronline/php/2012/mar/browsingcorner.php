				<img border="0" src="images/browsingcorner-mar-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3">The 
				Facebook Effect by David Kirkpatrick chronicles the vision, 
				determination and foresight behind Facebook’s phenomenal rise. 
				An OER review<br>
				<br>
				In the summer of 2006 Kirkpatrick, a former technology writer 
				for Fortune, found himself invited to dinner with Facebook’s 
				youthful CEO, Mark Zuckerberg, as part of the company’s charm 
				offensive. He subsequently won unprecedented access to 
				Zuckerberg and 39 of his top employees to gather material for 
				this book, a kind of official history of the company and the 
				most comprehensive account of its rise yet. The reporting by 
				Kirkpatrick is meticulous and exhaustive. The reader learns 
				precisely what percentage of equity Facebook’s biggest 
				shareholders have and exactly which major technology and media 
				companies have courted Facebook over the years for both 
				investment deals and potential acquisitions.<br>
				<br>
				<b>The early years</b><br>
				Facebook began life in a student room in 2003, and its original 
				function was to enable Zuckerberg and his fellow Harvard 
				students to rate each other’s attractiveness and flirt with each 
				other electronically; thus was born Facebook’s signature double 
				entendre, the poke. Before long Facebook had morphed into an 
				all-purpose public facility, allowing students to huddle 
				together in groups and forge whatever electronic connections 
				they liked. To Zuckerberg’s surprise – he suspected it might be 
				a fad – Facebook rapidly spread to other universities, then to 
				schools and then to everyone else. The statistics are 
				mind-boggling.<br>
				<br>
				Facebook’s arrival was timely, coming as it did just as more of 
				us got used to spending time hooked up to fast internet 
				connections. Kirkpatrick shows us how brilliantly Zuckerberg 
				polished his new machine, constantly cleaning its minimalist 
				look and cultivating its hunger for ever more data. The site’s 
				real engine of growth, though, was its built-in network effect. 
				Like any other communications network, Facebook’s usefulness 
				grew as more people signed up and found each other; that, in 
				turn, became a powerful incentive for new arrivals to pass the 
				word on.<br>
				<br>
				Previous books about Facebook, such as Ben Mezrich’s The 
				Accidental Billionaires, have focused on the squabbles and 
				personal relationships of those involved in its early life. 
				Kirkpatrick’s wide-ranging access allows him to pay more 
				attention to what Zuckerberg was trying to achieve. In his 
				sometimes oafish determination to realise his vision, Zuckerberg 
				turns out to be as much ideologue as engineer. For him, Facebook 
				is primarily a social movement, not a publishing platform: as he 
				tells it, he is motivated not by money (he consistently refuses 
				to sell up) but by a passion for radical transparency. Sharing 
				our data and making our lives publicly available to each other 
				turns us, he believes, into better people. A narrower gap 
				between public and private reduces the potential for hypocrisy 
				and connivance, making it harder, for example, for people to 
				cheat on their partners.<br>
				<br>
				Kirkpatrick’s story ends with the arrival of Sheryl Sandberg, 
				whose job it is to make Facebook more attractive to advertisers. 
				She has done well. Facebook makes the bulk of its money by 
				helping companies target potential customers more effectively 
				than mainstream media, and last year – the first that it began 
				turning a profit – it took an estimated $800mn in revenues. The 
				effect of all this on the marketing and advertising industries 
				has only just begun to be felt. Kirkpatrick has written the 
				definitive account of Facebook’s breathless rise to power. <br>
