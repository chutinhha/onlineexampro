						<font face="Verdana" size="4" color="#C00000">
						Treasured memories<br>
						</font><font face="Verdana" size="2">
                		<br>
						A team of four Swiss journalists won the first 
						international media treasure hunt event organised by 
						Oman Air to showcase that the Sultanate while being the 
						essence of Arabia is indeed an oasis of peace, stability 
						and security<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/tourism-mar-12-1.jpg" hspace="8" vspace="5" width="300" height="201"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">Oman Air, working with the 
						Ministry of Tourism, organised the first of its kind 
						International Media Treasure Hunt to showcase the 
						wonders of the Sultanate of Oman to some of Europe’s 
						leading media personalities.<br>
						<br>
						Instead of holding the regular familiarisation trips for 
						tour operators and journalists, Oman Air launched this 
						exciting action-packed event for journalists who were 
						able to discover as much as possible about the history, 
						culture, landscapes, nature and people of the Sultanate. 
						The indirect message through this event was that Oman as 
						a country is an oasis of peace and stability and 
						welcomes the discerning tourist to taste the country’s 
						rich and diverse natural beauty, culture and heritage.<br>
						<br>
						The first annual Oman Air International Media Treasure 
						Hunt pitted teams of journalists from Oman Air’s 
						European destinations of France, Germany, Italy, 
						Switzerland and the UK against each other in this 
						challenge.<br>
						<br>
						Each teams consisted of four journalists each, who were 
						flown in Oman Air’s award-winning business class from 
						their home countries to Oman to take part. They were 
						encouraged to use social media to call on their readers’ 
						and viewers’ assistance in solving the clues they are 
						given, and the whole event was filmed for broadcast at a 
						later date.<br>
						<br>
						Tremendous interest was generated through the social 
						media across Europe as the journalists gave blow-by-blow 
						account of their adventure. Regular updates were also 
						provided via Oman Air’s Facebook pages and Twitter 
						account, and on YouTube in five different languages.<br>
						<br>
						The search for information will be triggered by a series 
						of cryptic clues and will involve travel across the 
						length and breadth of the Sultanate, using every 
						imaginable form of transport, from taxis and hire cars 
						to aircraft, boats and camels.<br>
						&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/tourism-mar-12-2.jpg" hspace="8" vspace="5" width="300" height="201"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">
						<br>
						The adventure ran between February 6 and 13 and began 
						and ended with a night’s stay in one of Muscat’s finest 
						five-star hotels. In between those nights of luxury, 
						participating teams raced against each other as they 
						scoured the country for information and insights into 
						Omani life. A team of Swiss journalists emerged as the 
						winners. While launching the event, Wayne Pearce, CEO, 
						Oman Air, in comments to the media stated, “We are 
						delighted to be launching this unique event, which will 
						take the cream of European journalism on the tour of a 
						lifetime around Oman. They will discover for themselves 
						the breath-taking landscapes, unspoiled coastline, rich 
						culture and long history of this beautiful country, 
						experience its safety and serenity and enjoy the warm 
						hospitality of the Omani people.”<br>
						<br>
						Mohammed Al Shikely, general manager marketing at Oman 
						Air, in close coordination with Oman Air’s corporate 
						communication and media department, oversaw the launch 
						of this media treasure hunt. The Treasure Hunt covered 
						several locations in Oman and at each location there 
						were challenges of between 30 minutes and 3 hours. It 
						was stressed that each solution to a clue should be 
						discovered on camera. The journalists were trained on 
						how to use hand-held GPS devices.<br>
						<br>
						Kicking off day one of the challenge, the five teams – 
						from the UK, France, Germany, Italy and Switzerland – 
						paid a visit to Muscat’s Grand Mosque.<br>
						<br>
						Having toured this iconic landmark and received their 
						first clue, the teams then visited the capital’s 
						awe-inspiring Royal Opera House, before moving on to 
						Muttrah to see the souk and Bait Al Zubair, Oman’s 
						foremost museum of Omani history. Throughout the course 
						of the day, each team searched for solutions to the 
						clues they had been given and by the evening, it was 
						clear that the Italian team had taken first place, after 
						overtaking the UK’s early lead.<br>
						The second day of the challenge saw each team move to 
						Nizwa, where the city’s famed spice market provided the 
						solution to their first clue, in the form of dried 
						lemons. They then pushed on into the area of the 
						ruggedly beautiful Jabal al Akhdhar (Green Mountain), 
						where the hunt took the teams to the picturesque village 
						of Misfat al Abiyeen and its ancient Falaj irrigation 
						system.The next port of call was the vast Al Hoota 
						caves, where the solution to the day’s final clue could 
						be found. Having been given a head start at the 
						beginning of the day in recognition of coming first on 
						day one, the Italian team retained their lead and hung 
						onto the top spot as the sun set on day two’s activity.
						<br>
						&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/tourism-mar-12-3.jpg" hspace="8" vspace="5" width="300" height="201"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">
						<br>
						After a good night’s rest at the Sahab Hotel, the teams 
						began day three by crossing Al Wasil to Mintrib, in the 
						Sharqiya Sands, where, as well as continuing their 
						investigations, participants found time to photograph 
						the breath-taking scenery and race across the region’s 
						spectacular dunes.<br>
						<br>
						But it wasn’t all light hearted and both the Swiss and 
						Italian teams’ vehicles soon became stuck in the sand. 
						However, the Swiss team freed themselves first and 
						pressed ahead to become the day’s victors, before 
						joining with all the other participants to spend a night 
						under the desert stars in a Bedouin-style luxury 
						encampment.<br>
						<br>
						An early start on day four preceded a morning of camel 
						rides, dune trekking and a long drive to the lighthouse 
						at Sur, the ancient home of Omani Dhow-building. Clues 
						led the teams to the bow of a boat in Wadi Shaab, where 
						the next step of their marathon was to start.<br>
						<br>
						By the time each team had arrived back at the Shangri-La 
						Bar al Jissah Resort and Spa in Muscat, the battle for 
						first place was neck and neck, with the Swiss and 
						Italian teams sharing the honours. Each journalist 
						retired exhausted, but with memories of Oman that will 
						last a lifetime. The last 24 hours of the International 
						Media Treasure Hunt saw the five teams head south to 
						Salalah, the capital of Oman’s Dhofar Governorate.<br>
						<br>
						Here, they not only experienced the region’s renowned 
						hospitality and culture, but also its lush greenery, 
						steep mountains and deep wadis. Another unforgettable 
						day unfolded, with each team racing in four-wheel drives 
						to the huge Al Baleed archaeological site, where they 
						found the answer to their clue at the Merchant’s House.<br>
