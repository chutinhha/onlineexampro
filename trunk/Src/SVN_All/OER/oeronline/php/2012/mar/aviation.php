						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Flying 
						High</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/aviation-mar-12-1.jpg" width="500" height="375" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				The Swiss International Air Lines unveiled its new plans and 
				strategies at a special SWISS Brand Experience Day. Rekha 
				Balakrishnan reports from Zurich<br>
				<br>
				<br>
				Swiss International Air Lines (SWISS), the national airline of 
				Switzerland intends to further strengthen its position and 
				profile as the airline of Switzerland over the coming years. 
				While maintaining a keen focus on key business competencies such 
				as cost management and flexibility, SWISS will also be striving 
				to further intensify its collaboration with the Lufthansa Group, 
				with a particular emphasis on sharpening its brand profile. The 
				new plans and strategies were unveiled at a special SWISS Brand 
				Experience Day held for the media at Zurich in January. <br>
				<br>
				<b>Lounges</b><br>
				The Brand Experience Day began with a trip to the lounges at 
				Zurich Airport. There are three-lounge complexes that offer 
				dedicated facilities for first and business class passengers as 
				well as for Miles &amp; More travellers. Elegant design, a luxurious 
				ambience, Swiss hospitality and carefully selected food and 
				beverages are all provided here along with modern meeting 
				facilities.<br>
				<br>
				<b>On board<br>
				</b>SWISS has set new benchmarks in business class comfort 
				throughout its Airbus A330-300 and A340 intercontinental fleet. 
				The concept also includes the new SWISS business seat, which 
				features a pneumatic air cushion whose firmness can be adjusted 
				to individual taste, and which can be converted into a totally 
				lie-flat two-metre bed. Culinary needs are amply met, too, in 
				the form of a five-course menu which – like its first class 
				counterpart – has been specially created by the finest Swiss 
				chefs. Throughout Europe and on long-haul routes, SWISS business 
				class provides quality service that includes separate check-in, 
				priority boarding and baggage delivery, lounge access and a wide 
				choice of carefully-selected inflight specialities. With the 
				introduction of its Airbus A330 fleet SWISS has also introduced 
				a new first class cabin that offers a genuine ‘suite above the 
				clouds’. On intercontinental flights, the economy class features 
				an adjustable seat headrest and swivel ears for optimum 
				long-haul comfort. On the Airbus A340 and the A330-300, the seat 
				is also provided with special ergonomic padding, together with 
				other facilities for a comfortable flight.<br>
				<br>
				<b>The food</b><br>
				The ‘SWISS Taste of Switzerland’ highlights the cuisine of a 
				region of Switzerland every three months and offers a totally 
				new selection of dishes that are created by a renowned Swiss 
				chef from the guest canton served in SWISS First and SWISS 
				business class on intercontinental flights. For business class 
				passengers on European flights, SWISS recently introduced the 
				‘SWISS Traditions’ programme that offers classic and popular 
				Swiss delights together with dishes that have their roots in 
				famous national and cultural events.<br>
				<br>
				<b>New logo and positioning<br>
				</b>The new logo shows a SWISS tailfin with its Swiss-cross 
				design, reflecting the livery of the company’s aircraft and 
				enabling SWISS to be instantly recognised as ‘The Airline of 
				Switzerland’ even from afar. SWISS has also further distilled 
				its commitment to the customer to deliver a top-quality service 
				product into a concise new claim: “Our sign is a promise.”<br>
				<br>
				The SWISS brand positioning statement includes the following:<br>
				Our Swiss commitment to quality means we can deliver a 
				consistent, high-grade product and service at all touch points.<br>
				<br>
				Because we are smaller, we can better emphasise personal service 
				and customer intimacy.<br>
				Aviation<b><br>
				<br>
				Connecting Switzerland to the world</b><br>
				SWISS serves 72 destinations in 38 countries from Zurich, Basel 
				and Geneva and carries some 15 million passengers a year with 
				its 89-aircraft fleet. It operates 26 long-haul and 36 
				short-haul aircrafts and 20 regional Avro RJ100 aircrafts. The 
				Avro Europe fleet will be replaced through 30 Bombardier CSeries 
				100 as of 2014. The airline will receive delivery of five 
				aircrafts of A320 family between 2012-14 and five A330-300 
				aircrafts in 2012-13. <br>
				<br>
				OER spoke to Dr Holger Hätty, chief commercial officer at SWISS 
				to know more on competition, cost management and flexibility and 
				eco-friendly initiatives. Excerpts:<br>
				<br>
				<b>Oman Air has recently started its service from Muscat to 
				Zurich. Will it make a huge difference to SWISS’s performance in 
				the region?</b><br>
				Yes, Oman Air is definitely a competition. But to be frank, I 
				don’t adapt my product to Oman Air’s entry. We have a clear 
				philosophy and live by it. I don’t think we have to be afraid of 
				the competition. Oman is a small country where tourism is 
				becoming popular, and if other airlines are flying there, there 
				will be a lot of focus and attention and that will be good for 
				us. <br>
				<br>
				<b>Can you elaborate more on your focus on ‘cost management and 
				flexibility’? Is it because of the downturn in Europe? Will it 
				impact your services in any way?<br>
				</b>Cost-saving is a must as we cannot always save ‘on the back’ 
				of the customers and have to find a balance. We still have to 
				invest in customer and administrative processes, for example, 
				and, we have to bring modern aircraft which is again a huge 
				cost. So you have cost measurements on a short-term. Modernising 
				the fleet takes a couple of years. SWISS went through a 
				turnaround five years ago and during that time, we really tried 
				to keep our costs low. Our headquarters is very efficient, top 
				managers like me are working and are not delegating until it’s 
				at the lowest level.<br>
				<br>
				<b>Are you looking at new destinations in the near future? <br>
				</b>To finalise a new destination takes up to one-and-a-half 
				years. This year, we deploy two more long-haul aircrafts, one to 
				our new destination Beijing and the other to Newark. Next year, 
				we’d be adding another two. Right now, it’s at the decision 
				making stage as to where they are going to fly. And we do have a 
				long list of destinations.<br>
				<br>
				<b>Can you elaborate more on your fleet?<br>
				</b>We have an all Airbus fleet. Low cost airlines have a 
				competitive advantage in the sense that they have single fleet 
				type of aircraft. If you mix, you lose the synergies. We have an 
				Airbus fleet that is well-structured. We only have three 
				aircraft types and because of this, it’s very cost-efficient and 
				easy to manage when it comes to pilots, mechanics and the 
				economies of scale.<br>
				<br>
				<b>You also call yourself an environmentally friendly airline…<br>
				</b>We strive to buy the most modern aircraft in the market that 
				also have a lower fuel consumption. Fuel is a major cost factor 
				in the airline industry. If you have low fuel consumption, it’s 
				good for your balance sheet and for the environment as well. 
				Secondly, we also push politicians for the right framework and 
				policies that will help us in our eco-friendly initiatives. For 
				example, Lufthansa recently completed a project where it tested 
				biofuel on its aircraft between Hamburg and Frankfurt. The key 
				objective of this project was to conduct a long-term test during 
				which the impact of biofuels on the maintenance and efficiency 
				of jet engines can be investigated. So we are working on these 
				initiatives on a group level as well.&#8194;<br>
