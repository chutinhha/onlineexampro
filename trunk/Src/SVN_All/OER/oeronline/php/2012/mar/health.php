						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Pursuit of excellence</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Emerging as the first hospital in the Sultanate to win 
						the prestigious Joint Commission International (JCI) 
						accreditation in recognition of high quality healthcare 
						standard, Muscat Private Hospital positions itself as 
						one of Muscat’s premier private medical facilities. An 
						OER report<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/health-mar-12-1.jpg"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
                		Marking an important milestone in Oman’s health care 
				sector, Muscat Private Hospital (MPH) became the first hospital 
				in the country to be accredited by the prestigious US-based 
				Joint Commission International (JCI). The accreditation was 
				awarded to the hospital following a rigorous on-site evaluation 
				by an international surveyor team of healthcare experts.<br>
				<br>
				“Being the first hospital in the Sultanate to get the JCI 
				accreditation means a lot for us,” says Murad Soner Kucukkaya, 
				hospital administrator. “Through this accreditation the JCI 
				tells the people that we are safe and we operate based on 
				certain topnotch standards. It demonstrates that we are 
				accomplishing our mission of becoming the leading provider of 
				healthcare in the Sultanate. We believe that this very important 
				designation reaffirms our commitment to the community as we 
				accord top priority to ensure our patients’ safety and to 
				provide quality healthcare services”. Murad urges other 
				hospitals in Oman to try to get the accreditation as it will add 
				to the quality of healthcare services in the country. “We 
				believe in healthcare professionalism. That is why I want all 
				other hospitals in the country to get the accreditation so that 
				it will be the society that will benefit at the end of the day. 
				If every hospital pushes them up, the health care system gets 
				better in the country.”<br>
				<br>
				“It is really a proud moment and it shows a visible commitment 
				towards quality and culture of excellence promised by MPH”, adds 
				Rozmin Nizar Punjani, quality education manager. “The 
				accreditation testifies to our commitment to reduce the medical 
				errors and infection rates, and to improve the safety and 
				quality of patient care. The evaluation has been a two-year 
				process where we attached top priority to ensure a safe care 
				environment and to reduce risks to patients and the staff. We 
				set up the policies to comply with 14 chapters of Joint 
				Commission and had a rigorous training programme to implement 
				them practically. Intensive training and awareness programmes 
				were done for the entire staff and we did internal audits also.”<br>
				<br>
				Says Dr Mohammad Al Obaid, head of ER, “It involved a lot of 
				complicated processes which includes how you enroll a patient in 
				the hospital, how you are recruiting your staff, how you keep 
				your medicines updated and how you employ different diagnoses 
				etc.”<br>
				<br>
				The surveyors spent four days interviewing staff and hospital 
				leaders on policies and procedures, looking in detail at 
				hospital care for individual patients. In addition, they 
				surveyed each department individually. The physical plant of the 
				hospital, equipment and services were subjected to evaluation. 
				Focus was also placed on aspects such as patient safety, patient 
				rights, medication management, policies and procedures, surgical 
				infection and credentialing of medical staff and nurses, 
				leadership of the organisation among many others.&#8194;<br>
