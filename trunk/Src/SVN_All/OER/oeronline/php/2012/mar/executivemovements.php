				senior manager, product development at Omantel corporate 
				business unit.<br>
				Announcing her appointment, Todd Dick, Vice President of Omantel 
				corporate business unit says: “Huda has been an asset to our 
				unit and to the company and we are glad to see her progressing 
				in her career. We are confident that Huda will bring incisive 
				management and leadership to this important function and help us 
				to grow and maintain our leadership position in the corporate 
				business sector.”<br>
				Commenting on her new role, Huda says,“I’m delighted to be the 
				first Omani woman to be appointed in such a senior management 
				role and looking forward to the challenges and responsibilities 
				in this new role. Omantel is a very dynamic and forward looking 
				company and I’m glad that I spent my whole career with Omantel.”<br>
