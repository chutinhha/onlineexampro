				fourth edition of Oman Debate, the national forum which focuses 
				on issues that are close to the heart of Omani people, is all 
				set to be hosted once again by Tim Sebastian, the founder and 
				chairman of ‘The Doha Debates’ and former BBC Hard Talk 
				presenter.<br>
				<br>
				Block your calendars for December 3, 2012, the date on which 
				Oman Debate, the most awaited annual corporate event in the 
				business calendar, will be held at the Al Bustan Palace Ritz 
				Carlton Hotel.<br>
				<br>
				The annual national debate along with the awards for Oman’s Top 
				20 companies is organised by OER, the leading business magazine 
				in the Sultanate, in association with the Capital Market 
				Authority (CMA).<br>
				<br>
				The Topic of this year’s Debate is – ‘Is Oman Future Ready?’ The 
				Oman Debate will raise and address issues like job creation, 
				improving standards of education and the private sectors role in 
				the development process. Anchored by Sebastian, an eminent panel 
				of leaders from the government and corporate sector will share 
				their views with the august gathering consisting of the who’s 
				who of corporate, government representatives and members of the 
				civil society.<br>
				<br>
				The Oman Debate and OER Top 20 Awards is supported by Nawras and 
				ahlibank as strategic partners, while Sharq Sohar Steel Group, 
				Port of Salalah and Al Habib are the support partners. Times of 
				Oman and Al Shabiba are the official media partners. <br>
				<br>
				The Oman Debate will be held under the patronage of HH Sayyid 
				Tarik bin Shabib Al Said, editor-in-chief, OER and HE Abdullah 
				al Salmi, executive president, Capital Market Authority.<br>
				The debate encompasses key themes that form the business 
				environment of today and tomorrow in Oman and will be tackled in 
				a healthy atmosphere in order to show the way forward for all 
				sections of the society and give clarity of thought for policy 
				makers.<br>
				<br>
				Some of the core issues that will be debated include: job 
				creation, ease of doing business in Oman and measures needed to 
				bridge the gap between aims/intentions and ground realities.<br>
				The invitees would include over 350 business leaders and 
				decision makers from Oman’s corporate sector and the government.<br>
				<br>
				In 2011, the Oman Debate witnessed a no-holds-barred discussion 
				as government representatives, private sector leaders and the 
				public talked about the most pressing issues facing the country 
				revolving education, Omanisation and training under the topic 
				development versus stability.</td>
