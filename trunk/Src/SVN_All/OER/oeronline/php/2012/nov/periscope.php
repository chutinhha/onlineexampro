						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						What next for Brent crude and energy shares?<br>
						</font><font face="Verdana" size="2">
                		<br>
						The price of Brent crude will have a bearing on the 
						stock market fortunes of oil majors like BP, Occidental 
						and Schlumberger<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						Brent crude has been a rollercoaster since last summer. 
						After a supply shock rally that took Brent to almost 
						$120, the oil market has now concluded that the 
						sanctions noose and currency collapse will force Iran to 
						backdown on its nuclear programme. A surge in Saudi and 
						Iraqi production will offset any supply shock and the 
						World Bank’s pessimism on Chinese/Asian growth will hit 
						oil demand. Riyadh and Washington are both comfortable 
						with $100 Brent and no money printing from global 
						central banks or gunfire in Gaza or Aleppo/Hatay can 
						negate the sheer power of energy’s most critical 
						bilateral geopolitical equation. An oil spiral ($5 a 
						gallon, gasoline in Ohio?) on the eve of the November 
						election could cost Obama the White House – and so this 
						will not happen.<br>
						<br>
						Any investor who thinks oil prices are set up by supply 
						and demand obviously never heard of Gulbenkians, the 
						Texas Railroad Commission, Juan Pablo Perez Alfonso, 
						Saudi Aramco, Marc Rich, Vetol, the Kremlin’s 
						petro-oligarchs, NYMEX and IPE. My call? Brent trades 
						between $100 and $110 in the next three months. The US 
						and China are the world’s two largest oil consumers and 
						constitute one third of global demand.<br>
						<br>
						<b>Schlumberger’s valuation<br>
						</b>Schlumberger (SLB), the world’s preeminent oil 
						services firm, has had a mediocre 2012, with a February 
						peal near 81 and a June low at 59. It is now obvious 
						that Schlumberger will not reach its peak valuation 
						multiple of the past in the current oilfield services 
						cycle, since global growth, margins, ROE are all so much 
						lower. Yet, I cannot believe that we will remotely see 
						the 24–26 per cent earnings hit now priced into 
						estimates. Schlumberger’s international franchise, 
						particularly with the BRIC/EM/GCC state owned oil and 
						gas colossi was its traditional strength and argument 
						for a valuation premium. However, Baker Hughes and 
						Halliburton have scaled up their international 
						footprint, with aggressive pricing and key contract 
						wins.<br>
						<br>
						In any case, Schlumberger margins suffer as the 
						integration of Smith International means low margin 
						drilling fluids have a bigger revenue weight than in the 
						past. Schlumberger also is vulnerable to the clear 
						overcapacity in seismic, reservoir management and 
						natural gas/fracking. Can valuation multiples contract 
						for SLB? Sure. Weatherford has lost three multiple 
						points down to 11 times earnings as investors derated 
						its growth paradigm (four per cent revenue growth does 
						not cut it, even Halliburton offers six per cent CAGR 
						revenue growth!). So let me utter Texas Tea heresy: 
						Schlumberger valuation multiples at risk. So are the 
						shares. I believe the shares can well down to $60–64 in 
						the next three months.<br>
						<br>
						Occidental (OXY) is arguably the world’s highest 
						production growth super major (and a strategic partner 
						of ADNOC in Abu Dhabi’s Shah gusher field and Mubadala’s 
						but it guided towards a lower Gulf of Mexico rig count. 
						This is bad news for energy producers and bad news for 
						Oxy shares because it means production growth in North 
						America will slow.<br>
						<br>
						However, this is a high Brent beta shares and will fall 
						to $76–78 if Brent falls to $100 or lower. Yet this is a 
						stellar level to buy Oxy, the world’s best play on the 
						Permian Basin, California, Rocky Mountain onshore 
						fracking and Arab LNG/crude (Abu Dhabi, Iraq, Qatar, 
						Libya). My value zones for Oxy are buy at $76-77 and 
						sell at 100–102 in this oil cycle.<br>
						<br>
						<b>BP looks promising <br>
						</b>BP has been a disastrous investment since the 
						fateful April 2010 Deepwater Horizon disaster in the 
						Gulf of Mexico created the biggest oil spill since the 
						oil tanker Exxon Valdez ran aground in Alaska’s Prince 
						William Sound. BP has exited Russia, restructured its 
						global franchise under CEO Robert Dudley, settled more 
						than $20 billion in Macondo legal liabilities and sold 
						more than $35 billion in non-core oil, gas, pipeline and 
						downstream assets. BP is now the cheapest oil super 
						major in the world and I want to buy its New York ADR at 
						40–41 to benefit from several specific year end 
						catalysts. One, BP will settle with the US Justice 
						Department now that it has sold its operator fields in 
						the Gulf of Mexico. BP’s $38 billion divestment will be 
						completed next year. Two, BP valuation metrics relative 
						to Shell are far too low. BP reserves are valued at 11 
						barrels of oil equivalent (boe) while Shell is 20 boe. 
						In any case, BP trades at a mere eight times earnings 
						and offers a dividend yield of almost five per cent. I 
						believe BP New York ADR is a deep value buy at $38–40 
						for a $52–54 one year target.&#8194;<br>
