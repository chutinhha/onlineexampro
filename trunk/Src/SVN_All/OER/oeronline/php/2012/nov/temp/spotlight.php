						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Poised for RECOVERY</span></strong><p class="style15">
				<span class="style14"><strong>Housing needs in Oman are 
				projected to grow in leaps and bounds in the coming years</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/spotlight-oct-12-1.jpg" width="400" height="323" style="border: 1px solid #808080" class="style4"></font><p class="style15">
				<br>
				Oman’s housing sector is still in a nascent stage when compared 
				to its GCC peers, despite government taking a series of 
				initiatives to build affordable houses for its citizens. The 
				housing sector needs not only more involvement from large 
				developers, but also availability of ample mortgage finance from 
				lending institutions.<br>
				<br>
				With more than half of Oman’s 2.7 million people under the age 
				of 20, housing needs in Oman are projected to grow in leaps and 
				bounds in the coming years. The Ministry of Housing had 
				completed 2,000 low-cost housing units worth RO40 million last 
				year, while another 2,000 low-cost houses will be delivered in 
				2012, making it possible for those at the bottom of the pyramid 
				to own a house. However, this is not enough for meeting the 
				housing needs of the entire population, which include middle 
				income people.<br>
				<br>
				Apart from lack of finance for affordable housing projects, 
				slower approval processes create a challenge for the entire 
				residential market in Oman. Further, limited supply of land for 
				developers who build low cost houses poses another challenge.<br>
				<br>
				<b>Government spending <br>
				</b>In an effort to boost the affordable housing segment, the 
				government allocates RO80 million every year through different 
				housing assistance programmes. During the fifteen month period 
				(2011 and March 2012), 3,174 housing units with a total cost of 
				RO60 million were offered through the Housing Assistance 
				Programmes, besides providing interest-free affordable housing 
				loans to 400 families with a total cost of RO7 million. The 
				ministry of housing has been supporting and financing affordable 
				programmes for low-income and social security families through 
				social housing programmes.<br>
				<br>
				The Ministry of Housing provides adequate housing for families 
				through the Housing Assistance Programme, the Housing Loan 
				Programme and the Housing Units Project. The Housing Assistance 
				Programme aims at building and restoring houses for families of 
				social security and low-income, while the Housing Loan Programme 
				provides soft, free interest loans not exceeding RO20,000 for 
				Omani families.<br>
				<br>
				The number of families benefited from the Social Housing 
				Programmes since its inception and until the end of March 2012, 
				reached 29,600 families, with a total cost exceeding RO475 
				million. As much as RO360 million has been allocated for the 
				Housing Assistance Programmes during the current five-year plan. 
				The ministry aims at helping more than 18,000 families during 
				the current five-year plan period, while soft loans will be 
				offered to more than 2,500 families in the next three to five 
				years.<br>
				<br>
				<b>Shortfall of housing units<br>
				</b>Despite all these initiatives, there is still a shortage for 
				affordable houses in Oman, as majority of projects developed by 
				private real estate firms are integrated tourism complexes or 
				luxury apartments aimed at upper middle class customers. A study 
				in 2011 showed that Oman has an estimated shortfall of about 
				15,000 housing units. However, the extent of housing shortfall 
				in Oman is the lowest among seven countries in the Middle East 
				and North Africa (Mena) region. These countries have a combined 
				shortfall of more than 3.5 million affordable dwellings, as the 
				real estate industry has failed to provide sufficient housing 
				units to meet the required demand, said Jones Lang LaSalle in a 
				study report.<br>
				<br>
				Focusing on seven major markets, the report said that while 
				governments across the region are increasing their attention on 
				the supply of new homes, demand is far out-stripping supply as 
				the region experiences population growth around twice the global 
				average. With a young and fast growing population, the report 
				estimates that there remains a combined shortage of more than 
				3.5 million affordable dwellings across the major markets within 
				Mena region and that demand will continue to outstrip supply for 
				at least the next five years. The extent of this shortfall 
				varies from more than 1.5 million units in Mena’s most populous 
				Egypt, to just 15,000 units in the Sultanate. The major factors 
				that hinder the supply of affordable housing include high land 
				values which have reduced access to affordable land and high 
				capital costs for associated infrastructure development such as 
				electricity and sewerage.<br>
				<br>
				<b>High-end leisure<br>
				</b>Several property developers, including major layers like 
				Muriya, Muscat Hills, Alargan and the Wave, have massive 
				development plans at different stages of completion in various 
				locations spread across the country.<br>
				<br>
				Muriya, a joint venture between Egypt’s Orascom Development 
				Holding and Oman government’s tourism investment arm Omran, is 
				now developing two major integrated tourism complexes – Jebel 
				Sifah and Salalah Beach. Muriya’s Jebel Sifah and Salalah Beach 
				projects are part of integrated tourism complexes, which allow 
				100 per cent foreign ownership. Spread over an area of 6.2 
				million square metres, Jebel Sifah is home to luxurious freehold 
				apartments and villas of diverse architecture and style, four 
				five-star hotels and two marina boutique hotels, up to 200 berth 
				inland marina and marina town, retail venues and restaurants 
				cafes.<br>
				<br>
				Likewise, Salalah Beach is spread over an area of 15.6 million 
				square metres with 8.2-km beach front. This integrated tourism 
				complex will comprise of high-end luxury freehold apartments and 
				villas, its own shopping and retail outlets, five five-star 
				hotels and two marina boutique hotels, an 36-hole PGA Golf 
				Course, a 200-berth inland marina and a marina town, restaurants 
				and cafes.<br>
				<br>
				Muriya recently opened a 100-berth marina at its upscale Jebel 
				Sifah town, which overlooks the turquoise expanse of the Gulf of 
				Oman. The inland facility was unveiled at a special soft-opening 
				gala that also showcased Muriya’s ambitions to position Jebel 
				Sifah as a much sought after lifestyle destination and leisure 
				hotspot on Muscat’s shores. The new marina will serve as a 
				vibrant hub for yachters, recreational boaters and others with a 
				penchant for nautical adventure.<br>
				<br>
				Similarly, Alargan International Real Estate Company, the 
				Kuwait-based Real Estate developer, has launched Qurm Hills 
				project. The development, comprises of 109 plots, including road 
				networks, service facilities and landscaped park spaces 
				including a variety of residential units in addition to retail, 
				commercial and office space. The total size of the Qurm Hills 
				project spans 164,900 sq metres with a built up area is 119,000 
				sq metres, consisting of 109 plus plots. The average size of the 
				plot would be 1000sq metres.<br>
				<br>
				In yet another development, Al Khonji Real Estate &amp; Development, 
				which is popularly known as AQAR, is building their newest real 
				estate project Rimal. Once completed, Rimal will be one of the 
				largest of its kind in Muscat covering 50,000 sqm comprising 254 
				apartments, high-end leisure facilities such as two private 
				gyms, a choice of two swimming pools for children and adults, a 
				shopping mall, climate controlled corridors and three levels of 
				underground parking – making Rimal one of the most convenient 
				places to live in Muscat. The Wave Muscat and Muscat Hills, have 
				announced massive expansion programmes, which are expected to 
				enhance availability of luxury homes.<br>
				<br>
				The first phase of Tilal complex, which mainly include the 
				Muscat Grand Mall, residential apartments and office space, has 
				already opened for occupation. Developed by Al Madina Real 
				Estate, the project is the largest mixed use development in 
				Oman, combining a central place to live, a large shopping mall 
				offering a host of regional and international brands, 
				professional office space with the latest facilities, a health 
				club, hotel apartments and underground parking – all spanning 
				250,000 square metres. The second phase will include a five-star 
				business hotel with 300 rooms and forty luxury apartments, while 
				in the third phase, plans are afoot to expand the Muscat Grand 
				Mall.<br>
				<br>
				<b>Increasing over supply<br>
				</b>Meanwhile, the average rental values of residential 
				accommodation have fallen and a further reduction is foreseen in 
				the coming months in view of increasing over supply. The 
				residential leasing market in the Sultanate has seen a decline 
				in activity over a year. In fact, rising rental values and a 
				shortage of residential properties up until mid-2008 led to 
				significant development activity, which is now resulting in an 
				over-supply of residential rental property, according to a 
				survey conducted by Cluttons. There is a significant supply of 
				office space that will become available within the capital area 
				soon, which is likely to result in further reductions in rental 
				values. In early 2008, there was limited office availability 
				across the market, which resulted in demand out-stripping 
				supply. There is still considerable demand for office space in 
				Muscat, particularly from smaller (100 to 300 square metres) 
				space occupiers. The Cluttons survey found that Muscat currently 
				has approximately 300,000 square metres of retail space in 
				purpose-built retail centres.</td>
