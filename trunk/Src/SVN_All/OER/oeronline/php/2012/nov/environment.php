						exemplary track record within the voluntary field in 
						Oman. Most notably, this nomination was for her key role 
						in founding the Environment Society of Oman (ESO) – one 
						of the most active civil society organisations and the 
						only NGO for the environment in Oman – where she also 
						fulfils the role of president and is an active 
						participant in ESO projects and initiatives to raise the 
						awareness of environmental issues in Oman.<br>
						<br>
						On receiving the award, HH Sayyida Tania said, “I am 
						honoured to be nominated for this award which is leading 
						the way for volunteerism in the Arab World. This award 
						does not just recognise my personal contribution, it 
						recognises the contribution of all our dedicated board 
						members, staff and members at ESO who work tirelessly to 
						spread the word of ESO and who give up their time to 
						support our projects and initiatives. Thank you very 
						much to them and thank you to the Good Word Society and 
						Sheikh Issa for this honour.”<br>
						<br>
