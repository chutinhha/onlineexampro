						the Sheikh Issa bin Ali Al-Khalifa Award, said, “The 
						Award aims to honour the leading volunteers in the Arab 
						World who play vital voluntary roles in developing their 
						national societies. HH Sayyida Tania is a role model for 
						volunteerism in Oman; her fantastic achievements for 
						environmental awareness and protection have positively 
						impacted on Omani society as a whole, and have aided a 
						nation in learning more about their natural surroundings 
						and how to protect them.”<br>
