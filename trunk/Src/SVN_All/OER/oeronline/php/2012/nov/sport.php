						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					Cliff-hanger finale provides dizzying excitement</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The sultry 
					settings, virtually and figuratively, of Wadi Shab provided 
					the Red Bull Cliff Diving World Series 2012 a perfect ending 
					as the world’s best divers showcased their extreme sporting 
					skills in Oman. Visvas Paul D Karra reports</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/sport-nov-12-1.jpg" style="border-style: solid; border-color: #808080; " width="400" height="266"></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				Emerald-green water, dramatic rock formations of the Wadi Shab, 
				Omani passion and an ‘MTV-grind’ like atmosphere provided the 
				appropriate stage setting for the finals of the Red Bull Cliff 
				Diving World Series 2012.<br>
				<br>
				And in true blue edge-of-the-seat entertaining style, it was the 
				final three-second flights of the year at Wadi Shab, that 
				clinched the winner as Orlando Duque, of Colombia, who took the 
				last dive, came short at the points table.<br>
				<br>
				Ultimately, no one came close to Englishman Gary Hunt’s highest 
				score of the day – at 173.25 points – even though Duque tried 
				his best in the last dive of thrilling final showdown held at 
				Wadi Shab – arguably one of Oman’s finest adventure spots, a 
				canyon-like valley filled intermittently with turquoise waters, 
				two hours south of the capital Muscat.<br>
				<br>
				It was the red-hot final that everybody expected and it was a 
				battle until the very last dive. Leading the standings going 
				into the final round, Orlando Duque, of Colombia, had the honour 
				of taking the season’s last plunge at Wadi Shab, in the 
				Sultanate of Oman. Hunt, from Britain, had just set the day’s 
				high score of 173.25 points and the pressure was on the 
				Colombian. As soon as he had hit the water it was clear that the 
				higher degree of difficulty had paid off for Hunt, as the judges 
				awarded the same number of points to the two contenders, 
				crowning Gary Hunt with the World Series championship title for 
				the third time.<br>
				<br>
				Nearly, 5,000 enthusiastic spectators witnessed the most 
				thrilling final showdown and the first ever cliff diving 
				competition on Arabian soil in the presence of HH Sayyid Tariq 
				bin Shabib Al Said. In seven stops across Europe, North America 
				and the Middle East in 14 weeks, the cliff diving elite was put 
				to the test, awarding only the top five divers from 27 metres 
				with a ticket for the 2013 season.<br>
				<br>
				Almost 300 competition dives, seven different podium finishers 
				and four winners’ faces in natural, urban, remote and unique 
				venues made the fourth season of the Red Bull Cliff Diving World 
				Series the most awe-inspiring to date. The bottom six of the 
				2012 field of permanent participants will have to enter 
				qualification, together with new talent, in order to claim a 
				ticket for next season, early next year. Hunt had it in his own 
				hands on the September 27, 2012, and he did not disappoint. He 
				had to win the day’s competition to defend his title. The 
				‘brilliant Brit’ won by only 1.85 points, ahead of Orlando and 
				rookie David Colturi (USA).<br>
&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/sport-nov-12-2.jpg" style="border-style: solid; border-color: #808080; " width="400" height="365"></font><p align="justify" class="style13">
				<br>
				In a first statement, Hunt said: “This year has been the hardest 
				without a doubt. It felt like everyone was on top of their game 
				and you had to be diving really well to win it. Waiting for the 
				final score was one of the most nerve-wracking times of my life. 
				I saw that it was an amazing dive from Orlando and then saw the 
				scores but those few seconds were really nerve-wracking. I 
				didn’t want to calculate it before hand but to see it up on the 
				screen and how close it was... wow! It was just the best 
				competition I’ve ever done!”<br>
				<br>
				With a victory in the kick-off competition and five successive 
				second places, 38-year old Orlando Duque came back strongly 
				after sustaining a serious ankle injury: “I know if Gary’s 
				diving well then I have to do extraordinary dives to be able to 
				beat him and I got pretty close but it just wasn’t enough – 
				perhaps it was just half a point from one judge! I didn’t think 
				when I broke my ankle a year and a half ago that I would be in 
				this position fighting for the title on my last dive and I’m 
				proud of how I’ve performed. I want to raise my degree of 
				difficulty ahead of next year but you know that everybody else 
				will be doing the same.”<br>
				<br>
				Probably the most impressive newcomer, 2012 qualifier Steven 
				LoBue, of the USA, claimed his spot on the overall podium after 
				adding his name to the winners’ list at the sixth stop. 
				Completing the top five positions – the athletes who qualify 
				automatically for next year’s World Series – are Russia’s Artem 
				Silchenko and American David Colturi.&#8194;</td>
