						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Balancing varied interests<br>
						</font><font face="Verdana" size="2">
                		<br>
						Material Adverse Change Clauses hold great importance 
						for dealmakers in structuring a financing, and to 
						litigators in confronting financings gone wrong</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
						<i><font face="Verdana" size="1">By James Harbridge</font></i><b><font face="Verdana" size="2"><br>
						</font></b><font face="Verdana" size="1"><br>
						<br>Material adverse change (MAC) clauses are provisions 
						often found in loan and other financing documents which 
						allow lenders to refuse to fund if such a change occurs. 
						MAC clauses often hold great importance both to 
						dealmakers in structuring a financing, and to litigators 
						in confronting financings gone awry.<br>
						<br>
						The purpose of the MAC clause is to provide the lender 
						with protection such that if a major adverse change 
						(such as the types discussed below) occurs from the date 
						a financing agreement is signed, then an event of 
						default is triggered and the lender can essentially pull 
						out of the transaction and often demand immediate 
						repayment of any funds already lent to the borrower, 
						together with interest and any other costs payable.<br>
						<br>
						<b>What do MAC clauses cover?<br>
						</b>The definition and use of MAC clauses in financing 
						documents vary widely, depending on the type of 
						transaction, the market standards at the time of 
						negotiation, the relative bargaining powers of the 
						parties, and sometimes jurisdiction-specific factors 
						such as political and economic stability. It is common 
						to see lenders structure – and enforce – MAC clauses 
						more aggressively during economic downturns, forcing 
						borrowers to accept terms that they would otherwise 
						refuse.<br>
						<br>
						In facility agreements, for example, it is common to see 
						a MAC clause cover any adverse change within the 
						following areas, among others: <br>
						the business, operations, property and financial 
						condition of the borrower; the borrower’s or the 
						borrower group’s ability to perform its obligations 
						under the financing documents; the validity, 
						effectiveness, enforceability or ranking of any security 
						granted under the financing documents; and the 
						international financial markets.<br>
						<br>
						The MAC clause would typically provide that if a 
						material adverse change occurs in relation to any of the 
						above – this determination is often made at the lender’s 
						sole discretion – then an event of default is triggered 
						and the lender can stop the financing and demand 
						repayment of all borrowings and costs to date.<br>
						<br>
						<b>How are MAC clauses defined?<br>
						</b>The definition of the MAC clause is one of the most 
						important and heavily negotiated definitions in any 
						finance documentation, and can play a key role in legal 
						disputes over financings.<br>
						<br>
						Lenders typically seek to keep the definition as wide as 
						possible and may require that an event of default is 
						triggered if an event has a material adverse effect on 
						the borrower’s ability to perform any of its obligations 
						under the finance documents. Conversely, borrowers seek 
						to keep the definition as narrow as possible and will 
						seek to agree that an event of default is only triggered 
						if a material adverse change affects its ability to 
						comply with its financial covenants or payment 
						obligations under the finance documents.<br>
						<br>
						While many of the negotiations surrounding finance 
						documents often seem more theoretical than practically 
						applicable, the MAC clause is, in fact, a clause which 
						can be and is invoked. We have recently seen two cases 
						in Oman where Omani banks have invoked the clause, 
						causing the relevant borrowers to have to refinance 
						their loans as a result.<br>
						<br>
						Where a lender has successfully negotiated that it will 
						determine the occurrence of a material adverse change 
						“in its sole discretion”, it is usual for borrowers to 
						negotiate that the lender “act reasonably”. Borrowers 
						also seek to negotiate materiality qualifications, i.e., 
						that if a material adverse change has occurred that it 
						will have a material effect on a material obligation. 
						This is to avoid an event of default being triggered 
						over a trivial breach or the breach of a trivial term.&#8194;</font></td>
