						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Ahead of its time</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>Rajive Ahuja the man behind The Khimji Ramdas – Bright Sparks 
				blog has been winning plaudits for his initiative. An OER report</b><p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/beyondboardrooms-nov-12-1.jpg" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				Rajive Ahuja, head, Corporate Communication, Khimji Ramdas won 
				the Customer Service Professional of the Year certification at 
				the Oman Customer Service Excellence Awards 2012. He was awarded 
				the recognition for servicing customers using the Khimji Ramdas 
				– Bright Sparks blog. The fact that Khimji Ramdas is more of a 
				product driven company rather than a service oriented company 
				(unlike an airline or bank), makes the initiative that much more 
				special. Says Ahuja, “I am a customer Ombudsman within the 
				group.”<br>
				<br>
				“I have been with Khimji Ramdas for 10 years and have seen how 
				the milieu has been changing over the years. I realised that as 
				customers become more conscious, companies will need to pay more 
				attention to service and accountability. With 40 odd companies, 
				we needed a platform to reach out to customers before issues 
				escalated and became ugly,” says Ahuja. That’s when it occurred 
				to him that technology could be leveraged for connecting the 
				group with its customers. Based on this insight Ahuja started 
				the Khimji Ramdas blog. And the blog became a runaway success. 
				Today, it receives 400 views a day, with 62 per cent of them 
				being from Oman. Given its success, WordPress has put the blog 
				in its hallowed portal.<br>
				<br>
				<b>Multifarious uses<br>
				</b>The blog has matured into a conversational platform where 
				customers share their problems, appreciation and queries. There 
				have been occasions where the blog has helped in closing a sale, 
				as a tendering company was impressed by the group’s customer 
				service standards. The blog is also a digital tool that cuts 
				across all sections, enabling all kinds of people to be 
				connected with the management. Says Ahuja, “Our directors have 
				given their complete support to the blog as they look at it as 
				an investment rather than an expense.” <br>
				<br>
				The blog which started as a tool to inform people about various 
				Khimji Ramdas products, soon grew into a medium where people 
				could interact and share their problems regarding the groups 
				products and services. Once a customer voices a complaint, it 
				gets resolved within 24 hours. The moment an issue is raised by 
				a customer, it is sent to the concerned manager with the 
				corporate communication team doing a constant follow up till the 
				problem is resolved to the customer’s satisfaction. Says Ahuja, 
				“We do the update and maintenance of the blog in-house as it 
				enables us to attend to customer needs better. If we outsourced 
				it, there is a chance that the level of sincerity may falter.”
				<br>
				<br>
				<b>A mindset change<br>
				</b>There was some anxiety about problems and concerns being 
				dealt on a public platform, but the success and positive buzz 
				generated by the blog has emboldened everyone within the group. 
				Ahuja and his team are alert about not letting the conversation 
				degenerate into a slanging match. <br>
				<br>
				Encouraged by the success of the blog, the Khimji Ramdas group 
				has started a customer service centre with a toll free number. 
				Based in Knowledge Oasis Muscat (KOM), the centre receives 
				feedback and handles customer complaints on a day to day basis. 
				The centre receives 20-25 calls a day and follows up on customer 
				complaints till they are closed. It sends a report to the 
				corporate communications team twice a day. The centre has opened 
				up another channel for customers to connect with the Khimji 
				Ramdas group. Ahuja sums up, “The Bright Sparks blog and 
				customer service centre has led to a very strong 
				institutionalisation of a customer centric culture within Khimji 
				Ramdas, moreover these initiatives are not personality oriented 
				and will continue irrespective of the people who are managing 
				the process.”</td>
