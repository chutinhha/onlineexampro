						<font face="Verdana" size="4" color="#C00000">All-new 
						Hyundai Santa Fe launched in Middle East</font></b><p align="center" style="margin: 10px; ">
				<img border="0" src="images/automotive-nov-12-1.jpg" width="369" height="218"><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1"><b>The Middle East launch of the 
				all-new Hyundai Santa Fe was held in Oman. OER joins regional 
				and local journalists to test the car’s on-road and off-road 
				capabilities</b><br>
				<br>
				OTE Group, dealer of Hyundai’s range of vehicles in Oman, 
				launched the all-new Hyundai Santa Fe at Shangri-La’s Barr Al 
				Jissah Resort and Spa amidst much fanfare in October 2012.<br>
				<br>
				The new Santa-Fe is designed in a distinctive ‘Storm Edge’ 
				styling and is equipped with cutting-edge technologies and 
				practical use of space in a premium quality package to make it a 
				‘modern premium’ car in every way by setting new standards of 
				luxury and comfort in its class.<br>
				<br>
				With a choice of three engines, the option of two or four-wheel 
				drive, the Korean brand’s eye-catching new SUV is set to prove 
				that family motoring can still be fun. Global sales have so far 
				topped 2.5 million, and Hyundai is confident that the all-new, 
				3rd generation Santa Fe will win many new customers to the 
				brand, while appealing particularly to families looking for a 
				tough, versatile on-road SUV.<br>
				<br>
				Tom Lee, head of Hyundai Middle East’s regional headquarters, 
				said: “The all-new Santa Fe is the latest demonstration of 
				Hyundai’s ongoing commitment to producing modern, premium cars.” 
				He continues by saying, “The new generation Santa Fe will not 
				only appeal to existing customers in the region with its fresh 
				design and improved levels of standard technology, but will 
				attract a new range of Middle East buyers looking for a stylish, 
				premium and authentic SUV.”<br>
				<br>
				Following Hyundai’s new ‘Fluidic Sculpture’ design philosophy, 
				the all-new Santa Fe features a unique interpretation called 
				“Storm Edge”. This captures the strong and dynamic shapes 
				created by nature during the formation of a storm. The design 
				implements the trademark Hyundai hexagonal grille used in most 
				of its new line-up, creating a futuristic and urban-style SUV 
				image. The interior design is equally bold and modern, 
				especially in the front where the dashboard and centre console 
				exude a finish akin to that of a luxury modern sedan.<br>
				<br>
				The all-new Santa Fe will be available in the Middle East in a 
				sporty five seat form, with the option of two fold-down seats in 
				the boot. A long-wheelbase version with three proper rows of 
				seats will go on sale in 2013. The new model will offer a choice 
				of three powerplants: a new 3.3l V6 engine producing 270bhp and 
				up to 318Nm of torque, a 2.4l 4-cylinder unit delivering 176 bhp 
				and maximum torque of 227 Nm and a 2.2l diesel engine providing 
				up to 197bhp and 421 Nm of torque. Customers will have a choice 
				of 2 or 4WD, and six-speed manual or automatic transmissions.</font></td>
