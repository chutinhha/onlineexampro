						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">OER CEO Golf 
					2013, at the Almouj Golf course in The Wave Muscat, promises 
					loads of fun and surprises for both golfers and 
					aspiring-golfers</font></b><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo-13.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>OER 
				CEO Golf 2013, organised by United Media Services (UMS), is 
				already generating excitement as word gets around that Oman’s 
				premier corporate golfing event is going to be held at the Greg 
				Norman-designed Almouj Golf in The Wave Muscat.<br>
				<br>
				The interesting part of OER CEO Golf is that the event is open 
				for all business leaders, whether golfers or non-golfers so that 
				everyone comes together and gets to network in a friendly 
				environment. And as golfers attempt to reduce their handicaps, 
				the non-golfers get an opportunity to learn the basics of the 
				game and understand the joys of getting into serious golfing.<br>
				<br>
				This is the opportunity for real top-flight brands to be 
				associated with such a high-profile event in Oman as it’s become 
				the most anticipated up-market event of the year in Oman, and 
				every year, UMS tries to make it bigger and more exciting.<br>
				<br>
				The dawn to dusk event is planned in such a way that guests get 
				to unwind themselves at a gala prize distribution ceremony and 
				dinner in the evening which is laced with entertainment and 
				music.<br>
				<br>
				“OER CEO Golf is a unique networking opportunity every year for 
				the crème-de-la-crème of Oman’s corporate society amidst a game 
				of golf, providing the thrills of what it means to essentially 
				mix business with pleasure,” says Sandeep Sehgal, Chief 
				Executive, UMS<br>
				<br>
				Continuing further, Sehgal says, “We at UMS believe that OER CEO 
				Golf is now ingrained in the corporate calendar as an event 
				which kicks off the New Year. And all corporate leaders whether 
				it is a CEO or a business head should maximise the opportunity 
				to network with the who’s who of Oman’s business community over 
				a game of golf.”<br>
				<br>
				The sponsors for OER CEO Golf 2013 include Nawras Business which 
				is the main presenter while the support partners are Aggreko and 
				Times of Oman and Al Shabiba newspapers. Rolex from Khimji’s 
				Watches is the official time keeper.<br>
				<br>
				Talking about their association with OER CEO Golf, Mutassim 
				Hamood Al-Zadjali, department head, Corporate Affairs, says, “It 
				gives us great pleasure to be associated with OER CEO Golf once 
				again as the lead presenter of the event. Nawras Business has 
				been satisfied with the value created by this unique corporate 
				event by becoming the much sought-after corporate networking 
				event for golf loving business heads and leaders. We are 
				thankful to UMS for their efforts in keeping OER CEO Golf a cut 
				above the rest both in terms of helping the game to grow while 
				making it a pleasingly different event for driving business. 
				This endeavour is in line with the aims and goals of Nawras 
				Business which seeks to help customers to get more from their 
				business.”<br>
				<br>
				Following the natural lines of Muscat’s pristine coastline, 
				Almouj Golf at The Wave, Muscat is a links style course offering 
				amateurs and professionals alike a challenging, yet rewarding, 
				round of golf. Running alongside a six-kilometre stretch of 
				white beach, it is a 7,342-yard course of lush green grass 
				peppered with bunkers, water hazards and natural dunes. Designed 
				by Greg Norman, the course at Almouj Golf will provide a 
				challenging 18 holes of varying lengths and configurations.&#8194;</td>
