						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						In expansion mode<br>
						</font><font face="Verdana" size="2">
                		<br>
						Y Rajeev Reddy, Chairman and Managing Director, Country 
						Club India speaks to OER about the company’s performance 
						and future plans. A report<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/tourism-aug-12-1.jpg" width="400" height="304" hspace="8" vspace="3"></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<b>How is Country Club been doing and how was the company’s 
				performance in 2011? </b><br>
				We are doing pretty well in India, our turnover increased to 
				around INR378 crore (RO262.13mn), with a INR46 crore (RO31.88mn) 
				profit, which is an increase of over 10 per cent over the 
				previous year. We are looking at the European markets now and 
				are working on acquiring a property in London. <br>
				<br>
				<b>Europe is going through a financial crisis. Given this 
				scenario isn’t it a bit strange that Country Club is looking at 
				expanding there? </b><br>
				The recession is an opportunity for us, as properties in Europe 
				are available at a much more reasonable price and a number of 
				people would love to visit Europe from the Indian and Middle 
				Eastern markets. We want to use the global recession as an 
				opportunity. <br>
				<br>
				<b>How many properties and members does Country Club have right 
				now?</b><br>
				We have 55 properties out of which three are in Dubai, Sri Lanka 
				and Bangkok. We have 300,000 members and 12,000 people working 
				for the company. <br>
				<br>
				<b>Country Club has been a pioneer in the timesharing space, how 
				did the whole idea come to you?</b><br>
				I started the first club in Hyderabad in March 1989. The 
				property was the palace of the prime minister of Hyderabad. 
				Prior to that I was a builder and we had a strong name in the 
				industry. The club was started by the British for the British; 
				when they left it was being used only by the army and royalty. 
				These former clubs were still following the British ideology 
				with ballroom dances, a formal atmosphere and with children not 
				being allowed. It was totally alien to Indian culture. When I 
				became a corporate member of the regimental clubs, I realised 
				the need to form a club that had an Indian fabric. We wanted to 
				use the rich energies of the Indian culture. So when we started 
				we took members from different parts of India. We celebrated 
				Indian festivals like Navratri, Baisakhi, Lori, Christmas, New 
				Year’s eve and events became a big part of our brand Country 
				Club. The brand grew into different states in India. Now we are 
				in a big way in Bangalore, Chennai, Kerala, Mumbai, Delhi, 
				Kolkata, Ahmedabad, Surat and Poona. <br>
				<br>
				<b>How has the brand Country Club progressed over the years?</b><br>
				From a modest beginning of 400 members in the first year, we are 
				today at 300,000 families. All the top banks in India, today 
				fund the membership of Country club, because it is a win-win 
				situation for both members and the company. <br>
				<br>
				<b>Why should someone come and become a member of Country club?</b><br>
				You pay only once, and you are entitled for a holiday for upto 
				30 years. RCI is our global exchange provider, so our members 
				can visit their properties and vice versa. In areas where we 
				have our clubs, people visit us on a regular basis. A membership 
				provides you various options like holidays, clubbing facilities, 
				entertainment etc. We have recently started the great Birthday’s 
				Thumbs Up revolution. As a part of this initiative we call each 
				member on their birthdays and celebrate the occasion and their 
				photographs are put in the newspapers along with their telephone 
				numbers, so that others can call and wish them. Thus we are 
				building a lot of trust like a family.<br>
				<br>
				<b>What is the entry level fee for a membership? </b><br>
				In India the average fee is one and a half lakh rupees (RO1041), 
				and an annual fee of around INR 8,000 (RO55).<br>
				<br>
				<b>What kind of competition do you face in the Indian market?
				</b><br>
				In clubbing there is absolutely no competition. In timesharing 
				our offerings are the same as Mahindra Club Holidays and 
				Sterling Holiday Resorts. Our membership base is huge and the 
				number of our properties are two to three times that of our 
				closest competitor; moreover we are economical. We are 
				undoubtedly the leaders in the timesharing segment. <br>
				<br>
				<b>India is going through a slowdown; has this affected your 
				business?</b><br>
				There is a slowdown in the aviation industry worldwide but in 
				India the travel industry is booming. I travel at least 20 days 
				in a month and I find that our airports are as crowded as 
				railway stations. Our occupancies have gone up in the last few 
				years. We have taken two new properties in Goa recently, due to 
				the growing demand. We are increasing 100 rooms in Bandipur, 
				especially to cater to the growing number of visitors from the 
				Middle East. We have 180 affiliate properties and people using 
				our affiliate properties especially in the North and the East.
				<br>
				<br>
				<b>How do you choose the location of your properties and clubs?</b><br>
				We look into how easy it is for members to access it; that is of 
				prime importance. Most of the clubs are in the heart of the 
				city. Resorts can be anywhere, for example Bandipur is four 
				hours away from Bangalore. <br>
				<br>
				<b>What are your business model and revenue streams? </b><br>
				We have three revenue streams – monthly subscriptions, food and 
				beverages and membership revenues. All these add to our 
				revenues. In some places we have given direct sales associates 
				(DSAs) especially in Delhi, Mumbai and Poona, for marketing and 
				this is proving to be a successful business model.<br>
				<br>
				<b>What are your plans in the next one or two years?</b><br>
				We want to expand in Europe and the Middle East and we are 
				scouting for properties. Once we have finalised something we 
				will announce our plans. We are not just a timesharing company, 
				as these companies serve the customer only once in a year, but 
				we are there for our customers on 365 days a year. For example 
				all our health clubs are packed since the morning, in the 
				evening people come for a swim, play tennis, squash etc. and 
				have food. <br>
				<br>
				<b>Is there a new generation that is getting involved with the 
				business from your family?</b><br>
				Both my children are involved in the business. Y Siddharth 
				Reddy, the joint managing director is a professional finance 
				graduate from Texas and he looks after finances of the company. 
				Varun Reddy is the chief operating officer and he has a major in 
				communications. Our entire company is driven by technology and 
				customers can just go online and do the necessary bookings. <br>
				<br>
				<b>How closely are you involved with the business?</b><br>
				I am involved with the business on a minute to minute basis. In 
				the morning the first thing I do is to open my laptop and see 
				the messages that my secretary has sent me and I see all the 
				Birthday party pictures that have come from different locations.
				<br>
				<br>
				<b>How do you feel when you look back?<br>
				</b>I feel very thrilled.<br>
				<br>
				<b>What is your managerial style?</b><br>
				I am on the top of things and I interact closely with all my 
				people. I go to various places. I am involved with branding, 
				takeovers, marketing, new concepts, strategy etc. On the 
				operations side, we have professionals, so my involvement there 
				is limited to interacting with the various heads. I am also the 
				brand ambassador of the company so I have to go out and promote 
				the company. <br>
				<br>
				<b>What made you become the brand ambassador of the company?</b><br>
				All the famous Khans were asking for a fortune and I thought 
				that I should save some money for the company. I love adventure 
				sports, swim 365 days a year. I went scuba diving in Maldives, 
				hired a Japanese diver who is also a photographer and a Swiss 
				diver, they shot my pictures and that’s where it all started.</font></td>
