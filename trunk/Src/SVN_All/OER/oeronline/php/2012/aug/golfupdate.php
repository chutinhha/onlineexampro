						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Ernie Els 
					wins the British Open 2012 as Adam Scott collapses with four 
					bogeys in last four shots, suffering one of the biggest 
					chokes in golf’s history</font></b><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>South 
				African Ernie Els clinched his second British Open title, after 
				long-time leader Adam Scott spectacularly imploded at Royal 
				Lytham &amp; St Annes. It was Els’s fourth major championship. World 
				number 13 Scott, chasing his first win in one of the big four 
				events, started the last round with a four-shot lead and seemed 
				to be cruising to victory as he maintained that cushion with six 
				holes to play. But a late attack of the jitters saw him drop 
				strokes at each of the last four holes to allow South African 
				Els to slip in through the back door and lift the coveted Claret 
				Jug with a two-under-par 68 giving him a seven-under total of 
				273.<br>
				<br>
				Scott’s sad 75 meant he had to settle for second place on 274, 
				three shots ahead of former world number one Tiger Woods (73) 
				and his fellow American Brandt Snedeker (74).<br>
				<br>
				“I am just numb at the moment, I feel for Adam Scott, he is a 
				great friend of mine,” said Els, who picked up £900,000 in prize 
				money. “We both wanted to win so badly, but I really feel for 
				him. That is the nature of the beast, that is why we are out 
				here. It was my time. I was hoping at best a play-off. A lot of 
				people never thought I would win but I started believing this 
				year, getting a lot of help from my family and the professionals 
				around me.”<br>
				<br>
				Tiger Woods threatened at times but a triple bogey via a 
				greenside bunker at the par-four sixth and three straight bogeys 
				from the 13th sank his chances of a 15th major and first since 
				the 2008 US Open. The 36-year-old carded a three-over 73 to end 
				tied third at three under alongside playing partner and fellow 
				American Brandt Snedeker (74). <br>
				<br>
				The victory of Els has also ensured South Africa’s supremacy 
				over Australia in the Open record books. Both countries had nine 
				wins apiece before the 2012 Open and for a while poor Adam Scott 
				looked like bringing up the 10th for his nation by joining Peter 
				Thomson, Greg Norman, Lel Nagle and Ian Baker-Finch on the roll 
				of honour. But Lytham has been good to the South Africans – both 
				Bobby Locke and Gary Player have lifted the jug here, with Louis 
				Oosthuizen their other champion – and Els’s second title has 
				nudged South Africa into fourth place in the all-time Open list 
				with 10 wins, behind Scotland (41), the United States (41) and 
				England (22).<br>
				<br>
				<b>Autism fight goes on<br>
				</b>There was a real human story behind the golfing one as Ernie 
				Els became Open champion again at Royal Lytham. Between his two 
				victories 10 years apart Els and his wife Liezl have become 
				major fund-raisers after the discovery when he was seven that 
				their son Ben has autism. They moved their main home from 
				Wentworth to Florida and established the ‘Els for Autism 
				Foundation’. They also launched a campaign to raise £20mn for a 
				Centre of Excellence, committing £4million of their own money. 
				The land is now bought and it is hoped it will open in 2013.&#8194;</td>
