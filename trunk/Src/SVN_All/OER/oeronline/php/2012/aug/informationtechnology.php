						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Ushering in a new era<br>
						</font><font face="Verdana" size="2">
                		<br>
						A significant improvement in Oman’s standing on online 
						citizen and business services indices in 2012 
						demonstrates the success of government’s National e.Oman 
						strategy which seeks to transform the Sultanate into a 
						secure knowledge-based society. An OER report<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/informationtechnology-aug-12-1.jpg" hspace="8" vspace="5" width="300" height="225"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">For a country that set out 
						far later than most on the digital superhighway, the 
						Sultanate has a lot to show for itself, with some 
						prestigious international awards to boot. It’s a success 
						story that promises to pay rich dividends in the 
						creation of an e-enabled society where an array of 
						government and public sector services can be accessed 
						online.<br>
						<br>
						Oman’s quest to create a digital society is the essence 
						of the National e.Oman Strategy. It aims to transform 
						the Sultanate into a secure knowledge-based society by 
						implementing a set of initiatives that support the 
						rollout of e-government services. Spearheading this 
						drive is the Information Technology Authority (ITA), the 
						country’s flagship agency tasked with overseeing the 
						realisation of this vision. <br>
						<br>
						Significantly, the latest biannual eGovernment Survey of 
						the United Nations applauds the Sultanate on the rapid 
						progress it has made in the use of information 
						communication technology (ICT). Oman is ranked 16th in 
						the world on the UN eParticipation Index, which 
						indicates how governments create an environment in which 
						citizens can be more active and supportive of their 
						governments.<br>
						<br>
						The 2012 edition of the survey also points to 
						significant improvement in Oman’s standing on online 
						citizen and business services indices, which are 
						demonstrative of the success of the Omani government’s 
						and the ITA’s policies. The ranking for eServices this 
						year is at 35 compared to 55 in the 2010 Report.<br>
						<br>
						Similarly, Oman has improved its overall ranking on the 
						UN eGovernment Development Index, moving from 82nd 
						position to 64th out of 193 countries, advancing 18 
						positions from the 2010 Report. The Sultanate was listed 
						out of a total of 25 member states that offer separate 
						m-government sites. <br>
						<br>
						The e-government report evaluates components related to 
						online services, e-participation, telecommunications and 
						infrastructure, and human capital. Comprising the online 
						services index, four sections correspond to the four 
						stages of e-government development: emerging, enhanced, 
						transactional and connected. <br>
						<br>
						In the 2012 report, Oman has been evaluated at 57 per 
						cent in stage four, labeled ‘connected’, which 
						represents the most sophisticated level in online 
						e-government initiatives. This attests to the increase 
						in available citizen-centric services and the 
						government’s willingness to give citizens a voice in 
						policymaking.<br>
						<br>
						As measured by factors that focus on commitment to a 
						whole-of-government approach, Oman is listed with 
						several other countries among the top performers. Oman 
						was also listed among countries with government websites 
						providing a statement ‘follow us on Facebook or 
						Twitter’.<br>
						<br>
						The United Nations eGovernment Survey 2012 highlights 
						the speed with which governments across the world are 
						adopting technology to enhance public sector 
						efficiencies and streamline governance systems to 
						improve service delivery and support sustainable 
						development. <br>
						<br>
						Also highlighted by the UN survey is how governments are 
						using technologies like computers, tablets and mobile 
						phones to improve service delivery to citizens. Mobile 
						applications for car parking in Muscat, and the Ministry 
						of Education’s portal are all examples of this trend – 
						examples which have won national and international 
						acclaim in recent years.<br>
						<br>
						Among the ministries and government departments that 
						have won international honours in this regard are Muscat 
						Municipal (2003), Royal Oman Police (2009) Ministry of 
						Health (2010), the ITA, Ministry of Education, Ministry 
						of Civil Service and National Association for Cancer 
						Awareness (2011), and more recently the Health Ministry.<br>
						<br>
						<b>e-Business services</b><br>
						Yet another much-lauded initiative is the One-Stop-Shop 
						of the Ministry of Commerce and Industry. The creation 
						of this system arose from a conviction by the government 
						of the importance of investment in all economic fields, 
						by providing support to investors and businessmen, 
						regardless of their nationalities, and eliminating all 
						obstacles that limit the work of this sector. This 
						one-stop-shop system includes the provision of 
						e-business services to the business sector through 
						modern technology, which has approximately more than 60 
						completely automated e-services in various ministerial 
						sectors (i.e. trade, industry, minerals, and s on).<br>
						<br>
						Since its creation, the One-Stop-Shop system has 
						witnessed an unprecedented influx of requests and 
						transactions being submitted through the system. The 
						number of applications and transactions is growing at an 
						annual rate of about 15 per cent, with the tally for 
						2011 pegged at 223,091 submissions. <br>
						<br>
						Other ministries and government departments too have 
						rolled out innovative e-government services, some of 
						which won top billings at the GCC e-government awards 
						hosted by Kuwait in November, 2011. <br>
						<br>
						The Central Bank of Oman’s cheques scanning system won 
						the best transactional e-service award in the business 
						sector, while the educational portal of the Ministry of 
						Education scooped the best e-portal award in the 
						developmental sector. Winning the Best 
						Government-to-Government Project Award was the Tender 
						Board for its novel e-bidding system. Furthermore, both 
						the Ministry of Civil Service’s human resources 
						management and central recruitment system and the 
						Ministry of Higher Education’s higher education 
						admission system were honoured with two awards.<br>
						<br>
						Also keeping pace with the times has been Oman Post, the 
						state postal authority, which recently introduced the 
						landmark e-Post initiative. Billed as the 
						first-of-its-kind in the world, the initiative involves 
						the convergence of both digital as well as printed mail 
						into one unique digital address with each individual, 
						both national and expatriate, receiving a distinctive 
						identity free for life. Besides offering convenience and 
						enabling hassle-free transactions, the new e-post 
						identity will travel along with the person wherever he 
						resides around the country.<br>
						<br>
						Developed by Oman Post in collaboration with Vantage 
						Post Technologies BV of the Netherlands – a global 
						leader in digital document portals – e-Post also offers 
						every individual a virtual post box as well. It aims to 
						serve as a channel of communication between individuals 
						and government departments so that applications and 
						other processes are simplified. Further, as virtual box, 
						it will allow individuals to receive real printed mail 
						with notifications through their e-post ID.<br>
						<br>
						<b>Human capital</b><br>
						Importantly, the development of human resources is one 
						of the main pillars of the e.Oman strategy. In line with 
						this goal, the ITA has launched the National IT Training 
						and Awareness Initiative, which aims to train members of 
						the society – male or female – through 19 Community 
						Knowledge Centres spread across the Sultanate. Training 
						covers the basics of computer, enabling the candidates 
						to browse the Internet and to expand their knowledge 
						about different aspects of life.<br>
						<br>
						Omani women have been able to enjoy a bigger share of 
						this Initiative than men as they can not only avail 
						themselves of the training courses organised at 10 
						Community Knowledge Centres allocated to both genders, 
						but also undergo training through another nine Community 
						Knowledge Centres, exclusively set up for women within 
						Omani Women’s Associations. Statistics show that out of 
						the 30,000 citizens who underwent training through these 
						centres, 16,666 were women.<br>
						<br>
						And in a bid by the government to foster the pursuit of 
						excellence in the delivery of e-government and online 
						services, the ITA has instituted the Sultan Qaboos Award 
						for Excellence in e-government. This year’s contest 
						builds on the successes of the 2010 and 2011 editions to 
						throw it open – for the first time – to the private 
						sector as well. <br>
						<br>
						The Sultan Qaboos Award for Excellence in eGovernment 
						aspires to recognise qualitative transformation in the 
						field of eGovernment services by honouring digital 
						projects that deliver exceptional innovation and 
						achievement in the field of information technology. The 
						award aims also to honour excellent performance, apply 
						international standards in evaluating the degree of 
						development in e-services, support Oman’s digital 
						strategy, as well as strengthen IT and 
						telecommunications in the fields of economic, social and 
						cultural development.&#8194;</font></td>
