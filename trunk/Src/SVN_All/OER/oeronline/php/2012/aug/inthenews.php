						<font face="Verdana" size="4" color="#C00000">
						Opening new avenues</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						al izz islamic bank has launched its corporate identity, 
						as an important showcase of its progress ahead of the 
						upcoming IPO</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-aug-12-1.jpg" width="460" height="306"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				al izz islamic bank (under formation) recently unveiled its 
				corporate identity, marking the establishment of an ambitious 
				financial group that will be focused on offering 
				Sharia-compliant financial solutions to Oman’s corporate and 
				retail banking customers. The launch is an important showcase of 
				the bank’s progress and is the first public announcement as it 
				prepares for its initial public offering (IPO). al izz Islamic 
				bank aims to be a leading participant in the emerging Islamic 
				banking industry, offering comprehensive Islamic banking 
				products and services.<br>
				<br>
				Unveiling the logo, His Highness Sayyid Taimur bin As’ad Al Said 
				observed, “This is a unique opportunity for us to fulfil the 
				unmet demand for Islamic financial products and services in the 
				Sultanate and to contribute to the development of Oman’s 
				economy.Being one of the first Islamic banks, we hope to set 
				benchmarks in the Islamic banking sector by providing 
				customised, competitively priced and innovative Sharia-compliant 
				products, with the comfort and convenience of modern banking 
				technologies.”<br>
				<br>
				Speaking on the occasion, Jamal Darwiche, chief operating 
				officer (designate), al izz islamic bank said, “Islamic banking 
				will be the fastest growing banking segment in the local market. 
				Once launched, we expect this segment to grow rapidly, and 
				account for around 20 per cent of the overall banking market 
				within five years. al izz Islamic bank believes that the global 
				Islamic banking growth is likely to accelerate further with the 
				opening of new markets. Islamic banking assets with commercial 
				banks in Middle East and North Africa are now in excess of 
				$500bn. In GCC alone, more than one fourth of the banking system 
				is now Sharia-compliant. Oman is set to make history with the 
				imminent launch of Islamic banking in the country and the 
				estimated planned investment in Islamic banking system in Oman 
				could top $1 billion.”<br>
				<br>
				The bank is committing itself to building an inclusive financial 
				system, reaching out to the previously unbanked segments 
				including the small and medium enterprises in the country. 
				Further, it will be a catalyst to attracting Sharia-compliant 
				foreign direct investment for priority sectors such as 
				infrastructure, power, transportation and industry thus opening 
				new avenues for commerce and development.<br>
				<br>
				<b>Integral role </b><br>
				The name ‘al izz’ implies pride and prosperity. The logo is 
				inspired by an Islamic artistic pattern that captures the 
				traditional values of al izz islamic bank, while the colours of 
				blue and silver sets the bank’s tone of modernity.<br>
				<br>
				The silver calligraphy of the al izz islamic bank name in the 
				centre is symbolic of the integral role the bank plays in the 
				lives of individuals. The pattern radiating outward depicts the 
				bank’s influence and commitment to the larger community.<br>
				<br>
				Speaking on behalf of the founding shareholders, Mohamed Badawy 
				Al Husseiny, chief executive officer of Aabar Investments, said, 
				“We are delighted to be a cornerstone investor in al izz Islamic 
				bank. It will be one of our largest investments in the region.<br>
				<br>
				It may be recalled that the Central Bank of Oman (CBO) had 
				issued licences to two Islamic banks – Nizwa Bank and al izz 
				islamic bank – which are under formation and likely to start 
				operations as soon as the legal and regulatory framework for 
				Islamic banks is finalised which is expected any time after 
				Ramadan.&#8194;</font></td>
