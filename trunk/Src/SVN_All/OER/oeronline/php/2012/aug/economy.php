						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">Competition boosts 
						internet access</span><span class="style14"><br>
						<br>
						<br>
						</span></strong><b><font face="Verdana" size="1">
						<img border="0" src="http://www.oeronline.com/images/oliver_cornock.jpg" width="150" height="186" align="right" hspace="8" vspace="3">The 
						rising level of competition among telcos has changed the 
						dynamics of Oman’s telecoms market for good<br>
						<br>
						By Oliver Cornock</font></b><br>
						&nbsp;<p align="justify" class="style13">
						With the number of market players now increasing, Oman’s 
						telecommunications sector is starting to enjoy the 
						benefits of competition. This is not only expanding 
						internet provision, but also enabling the entry of new 
						broadband technologies into the sector. Indeed, it is 
						becoming clear that Oman’s connectivity market has rich 
						potential. Furthermore, ICT development and the spread 
						of high-speed internet are now seen as a key part of the 
						Sultanate’s drive to become a knowledge-based economy.
						<br>
						Traditionally, the country’s mountainous topography was 
						held to be the most obvious challenge to telecoms 
						companies, particularly in providing connectivity to the 
						more remote areas of the Sultanate. This is certainly 
						part of the reason why, in 2003, 167,500 Omanis had 
						internet access, whereas 1,105,000 UAE citizens were 
						online. Yet there was another factor behind the limited 
						internet coverage – one that is now giving way to make 
						for a thriving telecommunications sector. <br>
						<br>
						In short, and as might be expected in a relatively young 
						sector, the market suffered from a lack of competition, 
						with just one fixed-line internet service provider until 
						2005, when private operator Nawras was launched. The 
						monopoly of state-owned OmanTel in the landline 
						telephone and internet access markets up until that 
						point meant that the sector lacked the benefits of 
						competition – specifically higher service standards and 
						lower priced tariffs. <br>
						<br>
						The launch of the National Broadband Strategy in 2010 
						not only reaffirmed the Sultanate’s commitment to 
						delivering high-speed internet connectivity – a key 
						objective of Oman’s Vision 2020 programme – but was also 
						a clear message from the Telecommunications Regulatory 
						Authority (TRA) that enhancing internet penetration is 
						not an option for Oman, but a necessity. The benefits of 
						broadband were summed up by the World Bank’s recent 
						finding that a penetration increase of 10 per cent among 
						a national population can positively impact GDP by as 
						much as 1.5 per cent. <br>
						<br>
						<b>New Expansion <br>
						</b>More positively, two important developments have 
						meant that the telecoms market is entering a new period 
						of expansion. The first is a significant change in 
						market structure. Last year, mobile network operator 
						Sama Telecommunications was granted Oman’s third 
						telecoms licence in a decree issued by Sultan Qaboos bin 
						Said. Inevitably this had an impact on both Omantel and 
						Nawras, but it has been good news for consumers. With 
						the dynamics of Oman’s telecoms market changed for good, 
						strong competition among these providers has certainly 
						been a factor behind rapidly expanding broadband 
						coverage; between 1Q 2011 and 1Q 2012, fixed broadband 
						subscription increased by over 33,000 users to 88,164. 
						The penetration rate per household also increased from 
						9.92 per cent to 21.92 per cent over the same period, 
						according to the TRA. <br>
						<br>
						A second factor behind a healthier market has been the 
						advent of wireless and mobile broadband, which has 
						completely transformed the scope of telecommunications 
						and widened the platform for internet use. This is an 
						important factor for a country where, despite government 
						programmes to encourage computer literacy, and to 
						promote the use of computers, such as the Digital Oman 
						Strategy (e.Oman), access to computers remains limited. 
						According to the TRA, while demand for broadband grew by 
						31.5 per cent to reach 2.104 million users in the first 
						nine months of 2011, 1.654 million subscribers – or 78.6 
						per cent – accessed the internet through mobile 
						networks. Indeed, a recent report by Business Monitor 
						International predicted that subscriber growth will in 
						the main be driven by the mobile broadband segment in 
						the foreseeable future. <br>
						<br>
						However, all modes of internet use are set to benefit 
						from a further positive consequence of Oman’s growing 
						subscriber base and rising competition: the more rapid 
						take-up and implementation of the latest broadband 
						technologies – namely 4G. Whereas 3G (“Third 
						Generation”) was slow to get off the mark in Oman (3G 
						services were a late entry, with Nawras and OmanTel 
						winning their licences in 2007 and 2009 respectively, 
						whereas UAE internet service provider Etisalat has been 
						offering 3G broadband since 2003), now providers are 
						racing to be the first to offer 4G services. In late 
						June it was announced that OmanTel will shortly be 
						launching its 4G LTE (Long Term Evolution) network, 
						offering vastly improved internet speeds. That month 
						Nawras also announced that it will be launching its own 
						4G LTE service by the end of 2012, which will be 
						introduced according to where market demand is highest, 
						starting with the Muscat and Batinah regions, home to 
						more than 55 per cent of the population.<br>
						<br>
						<b>Enhancinng Connectivity<br>
						</b>The rising level of competition is also pushing 
						Omani firms to think outside the box in terms of target 
						markets. In a bid to stay ahead of heightened 
						competition, OmanTel is now looking to cast its net 
						wider. This is taking the form of enhanced integration 
						with the neighbouring UAE. OmanTel’s recent deal with 
						Etisalat, signed in June, is set to enhance connectivity 
						options, overseeing the installation of the latter’s 
						global multi-protocol label switching node in Oman. The 
						net result of this will be to enable Etisalat’s 
						customers to connect their offices (via Etisalat’s 
						services) between UAE and Oman networks, giving both 
						companies greater coverage. Indeed, deepening telecoms 
						integration with the wider Middle East – where mobile 
						internet subscriptions, for example, grew at a rate of 
						69,230 new connections per day in the first quarter of 
						2012, according to Ericsson – represents a proactive and 
						positive development. This partnership will allow 
						OmanTel to expand operations into a market that is 
						outpacing North America and Western Europe for 
						subscriptions growth.<br>
						<br>
						According to Mohammed Bin Ali Al Wahaibi, Undersecretary 
						at the Transport and Communications Ministry, the 
						government sees broadband as a key part of the national 
						plan to facilitate social and educational development – 
						part of the Sultanate’s goal to improve economic 
						productivity and access to information, particularly 
						against the backdrop of a move away from dependence on 
						oil and gas. Much is made of the importance of 
						high-speed internet for businesses and communication, 
						but the potential for facilitating the spread of 
						information to build a knowledge-based economy is clear.
						<br>
						<br>
						The importance of good broadband coverage, therefore, 
						cannot be underestimated. In June, it was announced that 
						Nawras had successfully linked the Ministry of Education 
						with a fast Internet connection to support every state 
						school with quicker access and improved data 
						connectivity. Furthermore, over 140 state-owned schools 
						across the Sultanate have been connected to Nawras 
						Internet Professional service delivered via WiMAX 
						technology. The fact that the government itself chose 
						Nawras over its own telecommunications company is a 
						testament to the success of liberalisation in creating a 
						healthier, more competitive and vibrant local market.&#8194;</td>
