		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<b>Training imparts physical and mental stimulation to your dog 
				and socialise them with other dogs, people and new environments, 
				writes Alfa Vold, Managing Director, Canadian Jebel K9 Training 
				Centre<br>
				&nbsp;</b></font></p>
				<p align="center" style="margin: 10px; "> 
				<img border="0" src="images/byinvitation-aug-12-1.jpg" width="460" height="306"></p>
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<b>
				<br>
				</b>All dogs, no matter what breed require guidance to 
				understand what is expected of them. Some dogs require more than 
				others and it can be stressful if you have a dog that is unruly 
				or displays behavioural issues. <br>
				<br>
				Training with your dog can be a very rewarding and fun activity 
				and when we approach it with a positive attitude your dog will 
				be become more attentive and look forward to training. <br>
				Make every interaction you have with your dog a training 
				session, for instance, during daily routines such as feeding, 
				play/walk time or grooming, all are enjoyable activities for 
				your dog and when you incorporate obedience exercises with these 
				positive interactions you are setting boundaries, providing 
				structure while making obedience rewarding for him. The result 
				is a dog that becomes more alert and focused on you. <br>
				<br>
				<b>Benefits of Obedience training<br>
				</b>Everyone enjoys a trained dog with good manners; giving him 
				fewer restrictions and more freedom.<br>
				<br>
				Training is a great way to exercise your dog. He receives 
				physical and mental stimulation.<br>
				Training classes socialise dogs with other people, dogs and new 
				environments. They learn to be calm and focused when exposed to 
				new sights and sounds in the future.<br>
				<br>
				Improves communication between you and your dog; your bond will 
				become stronger.<br>
				Creates mutual respect and demonstrates to your dog his rank in 
				your household. With some dogs this is not a problem however it 
				can be an issue to a dog who does not respect you as the 
				authority.<br>
				<br>
				Obedience gives you the necessary control in an emergency 
				situation. <br>
				<br>
				<b>Getting Started<br>
				</b>Puppies from two months old can be taught manners and basic 
				commands. Remember they get tired faster and sleep for longer 
				intervals than adults, and have shorter attention spans. Lots of 
				patience and constant positive reinforcement is the key.<br>
				<br>
				Dogs of any age can enjoy training. A few guidelines to remember 
				are consistency in rewards and timing, and using the same 
				commands for both good and bad behaviour. Consistency prevents 
				confusion and the dog learns faster. <br>
				<br>
				Work on one command at a time until it is mastered.<br>
				Start with short training sessions and build up gradually to 
				longer sessions each time. <br>
				Add a play session to mark a release at the end of training, 
				your dog will look forward to the sessions! This is easy to 
				accomplish when you understand what motivates your dog and use 
				this as his reward for obeying your command. <br>
				<br>
				Dogs that have no basic training are prone to be naughty and can 
				create a stressful environment for the owner and dog alike. A 
				dog that is prone to misbehaving in public can create anti-dog 
				sentiment, and can contribute to legal restrictions.<br>
				<br>
				The bottom line is that dog obedience training truly benefits 
				everyone. It is one of the most important aspects of raising a 
				dog and that investment will pay off as in the long run you will 
				have a happier dog that is more relaxed and a true pleasure.<br>
				<br>
