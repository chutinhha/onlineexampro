						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Time to buy oil &amp; gas shares?<br>
						</font><font face="Verdana" size="2">
                		<br>
						Slowing global growth means a protracted bear market in 
						crude oil<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						Saudi Arabia’s decision to pump and export the highest 
						crude volume since the Shah of Iran lost his Peacock 
						Throne three decades ago (Saudi production hit 10 MBD in 
						June), coupled with the return of Iraq and Libya to the 
						oil market, led to an increase in the supply of black 
						gold even as its demand curve moved lower due to 
						recession in Europe and a possible hard landing in India 
						and China. It was no surprise that Brent crude was 
						gutted from $126 in early March to below $90 in late 
						June.<br>
						<br>
						However, the EU embargo against Iran moves at least one 
						million barrels daily from the international oil markets 
						and has triggered a nationalist backlash in the Majlis, 
						the military high command and the Revolutionary Guards, 
						whose naval units have fired missiles and conducted 
						military exercises in the Gulf of Hormuz. There was a 
						Norwegian oil workers stike. This is the first supply 
						shock in the oil market since the Libyan rebels shut 
						down Colonel Gaddafi’s refineries, pipelines, storage 
						tanks and loading terminals in autumn 2011. As Brent 
						rises above $100, oil shares are natural candidates for 
						accumulation. Who? Why? Where?<br>
						I still believe slowing global growth means a protracted 
						bear market in crude oil, possibly Brent $75-80. After 
						all, the collapse in the Iran riyal, 25 per cent 
						inflation and social unrest could force the Ayatollahs 
						to compromise with the West on its uranium enrichment 
						programme, removing the geopolitical risk premium in 
						crude oil. This means it is still safer to buy low beta, 
						high yield, low valuation, integrated supermajors, such 
						as BP, Shell, Chevron and Total. Exxon Mobil’s gas 
						exposure via its takeover of XTO. Energy and its 
						chemicals business, reduce its correlation to crude oil 
						prices. I am less keen on offshore drillers, LNG, cola 
						shares and would avoid alternative energy shares like 
						the plague (aint nothing green about First Solar!).<br>
						<br>
						Oil services shares plummeted as Brent plunged $30 since 
						March as the financial markets feared that the fall in 
						oil prices would force exploration/production companies 
						worldwide to slash capex budgets. This was all the more 
						so since natural gas drilling in the Gulf of Mexico, 
						Texas and Oklahoma is under pressure and oil service 
						companies could well be victims of new downward EPS 
						revisions. However, it is unlikely that the US rig count 
						will drop much below 2000 in 2012 and state owned oil 
						and gas colossi, from the GCC national champions (ADNOC, 
						Qatar gas, Kuwait Petroleum, Saudi Aramco) to the 
						emerging oil and gas operators in Libya/Iraq, China’s 
						CNOOC and PetroChina, Brazil’s Petrobras and Russia’s 
						Rosneft, Lukoil and Gazprom will actually increase their 
						capex, as their sovereign status makes them immune to 
						short term seizures in oil prices or credit markets.<br>
						<br>
						However, as the global economy slows, a protracted 
						period of lower oil prices could dampen high cost 
						drilling projects in offshore West Africa, offshore 
						Brazil, the Russian Arctic and the Alberta tar sands. 
						This would be negative for oil services companies. 
						Schlumberger is the world’s leading oil services firm 
						and increased its footprint in deepwater after its 
						acquisition of Smith International. It is the most 
						global among the major US oil services firm. However, 
						Schlumberger has a lower exposure to the North American 
						natural gas/pressure pumping segment than Halliburton or 
						even Baker Hughes.<br>
						<br>
						Schlumberger is also the Texas (French?) oil service 
						firm most leveraged to the capex budgets of the world’s 
						leading state owned companies in the BRICS/major 
						emerging markets. EPS could well be in the $4 range and 
						I doubt if Schlumberger’s valuation multiples will fall 
						below 15 unless oil prices crash again. This means the 
						world’s leading oil &amp; gas services firm, whose shares 
						peaked at 95 in this crude cycle, has a value at 60-62.<br>
						<br>
						<b>Chinese slow down<br>
						</b>Lower electricity and fuel demand is telltale 
						evidence of a Chinese economic growth slowdown. The fall 
						in Brent and the slowdown in China energy demand/GDP 
						growth in a year of political transition (the Year of 
						the Dragon, no less!) mean the shares the CNOOC, China’s 
						leading offshore oil and gas exploration and production 
						companies lost a third of their value in its New York 
						ADR (symbol CEO) and its Hong Kong shares (883 HK). It 
						now makes sense to nibble at CNOOC shares in New York 
						and Hong Kong, since this Middle Kingdom Red Chip is 
						among the world’s most lean, mean operating machines in 
						Big Oil. This is a tribute to its low cost operating 
						base in coastal and offshore China, the home base for 
						almost 80 per cent of its offshore oil and gas platform. 
						CNOOC reserves are now valued in the Brent $72-82 range 
						and its dividend yield is a respectable 3 per cent, 
						making the shares compelling at eight times earnings 
						(the five year average multiple is 12X).<br>
						<br>
						CNOOC also trades at a mere four times its cash flow, 
						can increase production by 8-10 per cent and minimal 
						exposure to natural gas (one fifth of reserves and 
						production). However, rather than buy the shares of 
						CNOOC’s New York ADR at $200, I would prefer to sell put 
						options at the $180 strike whenever volatility in global 
						markets spikes higher.<br>
						<br>
						Conoco Philips, is in the midst of a strategic 
						restructuring. Conoco has sold its downstream assets and 
						transformed itself into a pure play oil and gas E&amp;P 
						firm, though I concede production growth is a mediocre 
						3-4 per cent. However, asset sales, oil royalty trusts, 
						share buybacks, rise in EPS growth will mean that the 
						shares of this undervalued Big Oil could rise on the 
						NYSE, possibly to as high as $78 in twelve months. The 
						ultimate dividend yield oil share! France’s Total, with 
						6 per cent.&#8194;&#8194;<br>
