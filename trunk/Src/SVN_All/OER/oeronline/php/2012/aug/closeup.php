						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Mighty Saudi Economy</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>With an extraordinary 28 per cent GDP growth in 2011, Saudi 
				Arabia alone accounts for 45 per cent of the GDP of all the GCC 
				countries put together<br>
				<br>
				By Dr Jasim Husain Ali<br>
				<br>
				</b>Numerous indications point to the steady growth of the Saudi 
				economy, thereby further strengthening the global position of 
				the kingdom’s gross domestic product (GDP). These factors 
				include relatively high oil prices, solid oil output, and strong 
				spending.<br>
				<br>
				To be sure, nominal or market prices of Saudi Arabia’s GDP 
				amounted to $577bn in 2011, or number 20 worldwide. This is an 
				exceptional achievement by virtue of placing Saudi GDP ahead of 
				several European economies including those of Sweden, Poland, 
				Belgium, Norway and Austria, to name a few.<br>
				<br>
				Undisputedly, Saudi Arabia’s GDP is the largest in the Arab 
				world. This partly explains the fact that the kingdom is the 
				sole Arab country in the G20, in turn comprising the largest 
				economies in the world. Speaking of the Arab world, the size of 
				the UAE’s nominal GDP is only second to that of Saudi Arabia. 
				With a GDP of $360bn, the UAE’s economy ranks number 30 on a 
				global basis, undoubtedly an extraordinary achievement. Among 
				many others, economic values of Denmark, Greece and Malaysia lag 
				behind that of the UAE.<br>
				<br>
				Saudi GDP grew by an extraordinary 28 per cent in 2011 mainly 
				due to developments in the oil sector.<br>
				<br>
				<b>Influence<br>
				</b>Yet Saudi Arabia stands out of enjoying some influence on 
				directions of oil prices by virtue of being the largest oil 
				exporter in the world. At the moment, only Russia produces more 
				oil than Saudi Arabia, but that could change in the current 
				socio-political environment.<br>
				<br>
				According to the BP Statistical Review of World Energy, an 
				authoritative publication in the field, Russia and Saudi Arabia 
				have output levels of 10.27 million and 10 million barrels per 
				day, respectively. In effect, this translates into Saudi Arabia 
				accounting for 12 per cent of oil output. Unlike Russia, the 
				vast majority of Saudi oil output is destined for export.<br>
				<br>
				It is believed that Saudi Arabia is capable of maintaining 
				sustainable output of 12 million barrels per day.<br>
				<br>
				This partly explains the kingdom’s ability to compensate the 
				international oil market for possible losses of output by other 
				oil producing nations such as Libya in 2011. The Libyan 
				revolution last year caused a temporary disruption in output, 
				largely met by rising production from Saudi Arabia. 
				Concurrently, Saudi Arabia has shown willingness to compensate 
				for the loss of Iranian oil due to sanctions imposed by the US 
				and the EU.<br>
				<br>
				Certainly, strong oil prices add strength to the Saudi economy. 
				In fact, oil prices having been hovering around $100 per barrel 
				for the past few years, certainly a new phenomenon. Happily 
				enough, the high oil prices are occurring at a time of ever 
				stronger oil output for the kingdom. Looking forward, steady 
				public sector spending provides a supportive hand. The budget 
				for fiscal year 2012 puts spending at $184bn, a conservative 
				figure when compared to actual spending of $214bn in 2011.<br>
				<br>
				However, if history is any guide, actual spending is likely to 
				be considerably higher, something put on display in fiscal year 
				2011. Conservative Saudi Arabia is noted for putting out notable 
				unadventurous statistics with regard to both revenues and 
				expenditures. In reality, the assumed oil figure for fiscal year 
				2012 can only be guessed, a questionable policy.<br>
				<br>
				On a negative note, the well-being of the Saudi economy remains 
				heavily at the mercy of developments in the oil sector. 
				Currently, the petroleum industry accounts for more than two 
				thirds of treasury income and exports as well as one third of 
				GDP.<br>
				<br>
				The challenge now lies in using the extra oil proceeds to 
				diversify the economy away from the petroleum sector.&#8194;<br>
