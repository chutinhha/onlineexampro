				by the Infinite 4WD.<br>
				<br>
				Just recently, Habiba drove her Evoque single handedly all the 
				way to Al Ain in the UAE for a wedding and back and she did not 
				even feel the strain. This car is made for the women folk, 
				states Habiba emphatically, while adding, “I like cars and I 
				like to drive fast and don’t feel tired even after driving for 
				long distances. I drove my Evoque to Al Ain just to test it and 
				I must confess that it was a smooth ride. And really it is a 
				very comfortable car.”<br>
				<br>
				What makes Habiba a proud owner of this Evoque is the fact that 
				everyone keeps asking about the car, its colour, shape of the 
				chairs etc. “To describe it in one word, it’s a very beautiful 
				and very powerful car. It’s not only luxurious but also has 
				quite a classic edge to it and a contemporary feel,” she says.<br>
				<br>
				There is no doubt that Habiba’s car is a rather striking Range 
				Rover Evoque Dynamic in red colour with a black roof and fitted 
				with Agility sets in red colour and special black multi-spoke 
				wheel rims with the same sleek, striking silhouette as the 
				coupé, with the added practicality and convenience of five 
				doors.<br>
				<br>
				The Evoque Dynamic is a sports-orientated take on the Range 
				Rover Evoque design theme with a more assertive, athletic 
				stance. Its attitude is emphasised by strong design cues. The 
				bold exterior, with unique bumpers, sills, grille and tailpipes, 
				is twinned with a premium sports interior with leather seats and 
				unique detailing. With an engaging blend of dynamic handling and 
				refined engineering, the Range Rover Evoque’s compact footprint 
				and advanced technology delivers exciting performance together 
				with the lowest fuel consumption of any Range Rover to date.<br>
