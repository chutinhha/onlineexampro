						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The value buy zone for Facebook and Apple<br>
						</font><font face="Verdana" size="2">
                		<br>
						Technology has the growth but it is not immune to risk 
						aversion. Value investors must, above all, be patient<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Facebook may command a $100bn plus valuation at its birth as a 
				public company but this will not last. The Instagram deal is a 
				strategic blunder. Facebook paid $1bn for a zero revenue 
				business that is not worth more than $300mn. This deal can only 
				have been done with Zuckerberg’s approval and a 27-year old 
				software developer is obviously no strategy genius by right. A 
				$100bn public company will be run by Zuckerberg as his private 
				fiefdom even after the IPO since he controls the voting rights 
				share. This is a corporate governance nightmare that could have 
				devastating consequences for Facebook shareholders in the years 
				ahead. What if Zuckerberg decides to write a $10bn cheque to the 
				next Stanford dropout with a hot social media website?<br>
				<br>
				I cannot compare Google with Facebook. Google went public in 
				2004 at a $23bn valuation, the reason it was a genuine growth 
				stock with a killer app on the Internet (search). This meant 
				there was ample potential for investors to buy Google in 2004 as 
				a long term growth stock and those who did so saw their shares 
				rise seven folds as Google rose from $85 to $650. The mathematic 
				of the Facebook IPO is entirely different. The big parabolic 
				increase in valuation happened during the eight years when it 
				was not a public company. <br>
				<br>
				This does not mean the shares cannot double in the next five 
				years. Yet as revenue slows, I believe Facebook valuation 
				metrics could get slammed. In any case, Facebook will trade at 
				100-120 times earnings at a time when Europe face financial and 
				political meltdown. This means the risk of a 30-50 per cent 
				crash in Facebook shares all too possible. Every earnings number 
				will be an appointment with a NASDAQ guillotine scaffold. Apple 
				stunned the world, delivered 50 per cent growth rates yet only 
				commands a 20 times valuation. Facebook is a speculative 
				colossus that could well fall victim to Greece’s exit from the 
				EMU later this summer.<br>
				<br>
				Facebook’s revenue model, while though powerful and unique, is 
				ultimately global online advertising sales. But Facebook is 
				Pepsi to Google’s Coke, the obvious dominant global champion. 
				While online ad sales is a high growth global business, Facebook 
				will never dislodge Google from the gold medal winner’s box, 
				despite Mark Zuckerberg’s hoodie on Wall Street roadshows. This 
				makes the 2011 revenue growth and EPS miss ominous. Facebook 
				earnings were near $1bn in 2011, and it has built a global brand 
				with 900 million users and can easily deliver 25 per cent EPS 
				secular growth. This means the downside risk in Facebook is 
				$50bn, not coincidentally the level it raised financing from 
				Goldman Sachs and Russian oligarch Yuri Miller. In essence, this 
				suggests a 15-18 range for Facebook makes a great long term buy 
				for value investors.<br>
				<br>
				<b>Iconic tech share<br>
				</b>Apple has fallen almost $90 from its $644 all time high. 
				This provides investors with a strategic opportunity to acquire 
				history’s greatest Big Tech ecosystem after Microsoft MS-DOS 
				cash machine. Apple is a growth share that dominates its 
				industry and the market penetration metrics in tablets, Macbooks. 
				Apple blew away the 1Q revenue and EPS estimates thanks to the 
				explosive launch of the iPhone and iPad in China. Annual EPS 
				growth was a phenomenal 92 per cent. China iPhone sales soared 
				fivefold and Apple is just beginning its footprint in the Middle 
				Kingdom, the world’s leading Internet market. The August 2012 
				put option on Apple with a strike of 530 can be sold to generate 
				a premium of $30. This means as investor can create a 
				risk/reward calculus in which the worst case scenario is being 
				forced to take delivery of Apple at 500 cost. This strategic 
				idea is only possible since Apple shares have corrected from 
				their highs while Chicago Volatility Index (VIX) has spiked 
				higher by 25 per cent since March. This means investors buy 
				Silicon Valley’s iconic tech share at a mere 10 times forward 
				earnings. This makes buying Apple at 480 – 500 for a $650 target 
				a credible value proposition for the next six months.&#8194;<br>
