				<img border="0" src="images/browsingcorner-june-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3" width="150" height="236"><b>Screw 
				Business as Usual argues how entrepreneurs can pursue a socially 
				beneficial business model without compromising on their 
				bottomline. An OER review</b><br>
				<br>
				Sir Richard Branson, one of the world’s most famous and admired 
				business leaders, argues that it’s time to turn capitalism 
				upside down-to shift our values from an exclusive focus on 
				profit to caring for people, communities and the planet. Branson 
				builds a strong case for the business world’s potential to 
				address social, economic, political and environmental issues by 
				creating new business models and new ways of doing business. He 
				does this not by giving some theoretical fluff, but rather by 
				stories of organisations and businesses that have done it. So 
				his narrative is planted firmly in the real world and that is 
				what is so inspiring and concrete.<br>
				<br>
				A business with a social conscience, he argues, will have more 
				motivated employees, save resources, drive higher profits, and, 
				paradoxically, be more satisfying than even the material wealth 
				it creates. The book reads like a series of loosely related case 
				studies and anecdotes that chronicle Branson’s jet-set 
				philanthropic life, from island-hopping with celebrities to help 
				third-world villagers. Now, as the head of the sprawling Virgin 
				empire, he tries to instill a social-impact component into each 
				new venture.<br>
				<br>
				<b>A wide canvas<br>
				</b>In one instance, Virgin Money, its retail banking arm, 
				offered up its transaction infrastructure to reduce the 
				credit-card fees that donors pay to runners-for-charity of the 
				London Marathon (reducing fees from six per cent to two per 
				cent). Branson writes that Virgin Money’s impact on the $100 
				million fundraising event illustrates how business can have a 
				much larger social impact than nonprofits. Branson’s book does 
				offer a few logistical reasons why socially conscious business 
				is good for the bottom line.<br>
				<br>
				Despite the underlying gravitas, the book is an easy read. The 
				big picture is built by narrating stories about new 
				organisations, leadership groups and businesses that are 
				combining business and social causes. The cases are mostly 
				related to what has been done in the Virgin Group, but also 
				include stories about people that Branson knows personally, 
				which includes a network of extraordinary breadth. He seems to 
				be able to call virtually any world leader, politician, 
				musician, movie producer or activist to form a team to deal with 
				issues ranging from healthcare to poverty to environmental 
				issues.<br>
				<br>
				This is a must-read book for anyone interested in business, 
				entrepreneurship, social issues, the future of capitalism, and 
				the current challenges that face the world today. Despite its 
				playful title, this is a substantive book that advises without 
				preaching. It is idealistic by providing real-world examples and 
				tells stories about famous people by focusing on their deeds and 
				not their fame. Overall Screw Business as Usual deals with 
				challenging issues and is an entertaining read at the same 
				time.&#8194;</font></p>
