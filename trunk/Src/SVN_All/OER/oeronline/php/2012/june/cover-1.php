				<img border="0" src="images/cover-june-12-6.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="350" height="349"></font><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Change and adapt<br>
				</b><br>
				Hard work and determination are two important requisites to 
				progress in one’s career, says Nabil Saif Al Jasri, Finance 
				Manager, OMRAN<br>
				<br>
				<b>Please give a brief description about yourself<br>
				</b>I am 34 years old and currently working as finance manager 
				with OMRAN (Oman Tourism Development Company). I completed my 
				graduation at the Sultan Qaboos University and did an MSc in 
				finance from the University of Strathclyde. My first job was 
				with BankMuscat as assistant manager – audit and was with them 
				for five years till 2005. During this period I completed my 
				master’s. Later I worked with HSBC and Oman Refineries and 
				Petrochemicals Company; and I joined OMRAN in June, 2011. 
				Through my career, I faced a number of challenges and I have 
				learnt that working as a team is very crucial and more 
				importantly I discovered that motivating the team is also 
				essential.<br>
				<br>
				<b>What was your experience with the NTI BizPro Young Achievers 
				Awards 2012?<br>
				</b>I have been selected among the top five of the NTI BizPro 
				Young Achievers Awards and it has been a completely rewarding 
				experience. It was an intense selection process which brought 
				out the best in me. The tests which were conducted as part of 
				the interview to select the top 10 gave me confidence to believe 
				in my capabilities. More than anything else, it was a 
				personality test for myself as they grilled us to know what kind 
				of knowledge depth we have. <br>
				As part of the tests, our logical thinking ability and how 
				updated we are on current affairs as well as our mathematical 
				skills were examined. In the second round, it was about our 
				managerial skills to check how we react to different situations. 
				Finally, there was a group discussion and an interview to see 
				how we can solve problems at work place.<br>
				<br>
				Before NTI BizPro, I didn’t have any inkling about my own 
				capabilities but now I have emerged stronger and I feel that I 
				can handle pressure and work in any field or industry.<br>
				<br>
				<b>You have risen up the ranks by your hard work and 
				determination. What is your message to your peers and the youth 
				of the country?<br>
				</b>I can say that only by sheer dint of hard work and 
				determination, one can achieve success. Ultimately, its all 
				about changing and adapting to situations. One cannot stay still 
				while the world around you is changing. Another thing is, our 
				youth need to, first of all, attain the right qualification and 
				exposure and then get the required experience to succeed in 
				life. Because I believe that in our country, all of us are equal 
				and everyone has an opportunity to become big if you want to. 
				You just need to motivate yourself and get the right attitude 
				for the job.<br>
				<br>
				<b>What are your plans for the future?<br>
				</b>I plan to go for a leadership training programme and 
				complete my certified management accountant (CMA) course. This 
				will give a further boost to my career and eventually, I hope 
				that I will be able to become a CFO.<br>
				<br>
				<b>Patience pays<br>
				</b><br>
				Nawaf Al Shahwarzi, Assistant Sales Manager, Al Bustan Palace - 
				A Ritz-Carlton Hotel has cultivated the right attitude to excel 
				in his chosen field<br>
				<br>
				<b>What made you chose the hospitality industry and what are the 
				challenges that this sector throws up for young executives?<br>
				</b>I joined the hospitality industry because it is a promising 
				one. One day we may run out of oil and there will be a need to 
				look at other sectors. The government is trying to promote 
				tourism in a big way and the outlook of the hospitality industry 
				looks really good. I see this as a good long-term career choice. 
				Hospitality is an attitude, you need to be at your best 
				behaviour while dealing with customers, so attitude is a big 
				part of getting the job right. Despite its challenges, I am 
				enjoying what I am doing. I have had a number of offers from 
				other industries, but being in a hotel is an everyday challenge 
				and I live that adventure.<br>
				You made it to the Top ten of the NTI BizPro Awards 2012, which 
				reflects your success at a young age. <br>
				<br>
				Having been through the NTI BizPro programme, what are your 
				thoughts on these awards? <br>
				I am really impressed by the NTI BizPro competition. It is a 
				challenging programme which strives to find the next generation 
				of young achievers or leaders of the future. It creates 
				awareness amongst the youth about working hard and looking up to 
				the future. Some of the analytical tests during the programme 
				taught me a lot and showed me the areas that I need to improve 
				upon.<br>
				<br>
				<b>Are there people who have influenced you or helped you by 
				being your role models? <br>
				</b>When I joined the hotel back in 2009, Al Bustan was run by 
				the Intercontinental Group. One of my general managers, Martin 
				Van Kam told me that he saw potential in me and gave me the 
				enthusiasm to go on with my hospitality programme. Van Kam asked 
				me once, “What do you want to be”? I replied, “I want to be a 
				manager.” Then he said, “No, you want to work on being a 
				manager.” This showed me that one has to work hard and grow 
				towards a managerial position. These simple words really 
				inspired me and he remains a role model for me. My father has 
				been my biggest support in my life; he told me about the 
				hospitality programme and encouraged me a lot. He has made all 
				my dreams come true.<br>
				<br>
				<b>Can you give us a brief on your educational and professional 
				background?<br>
				</b>I started working in Al Bustan Palace in June 2011 after 
				obtaining a diploma in hospitality from the Gilion Institute of 
				Hospitality, Switzerland. I did my first internship in this 
				hotel in 2009, I also did another internship in Montreal, Canada 
				at the Novotel Hotel. I joined the hotel as a sales executive 
				and I am handling government sales, banking and the public 
				authorities. I got promoted as the assistant sales manager in 
				January this year and that was a nice boost and recognition. 
				After doing my high school diploma from the Royal Guard 
				Technical College I worked with Gulf Air for two years in the 
				customer service division.<br>
				<br>
				<b>How do you spend your spare time? Are there certain hobbies 
				that you pursue?<br>
				</b>I had a weight problem as I used to weigh 180kgs. I lost 40 
				kgs within six months by regular exercise and following strict 
				diet regimen. Now it has become a daily habit; after work, I go 
				to the beach with my dog and walk for almost one hour. I now 
				weigh 138kgs and hope to bring it down further.<br>
				<br>
				<b>You are a role model for young Omanis, what advice would you 
				give to the youth of the country? <br>
				</b>My advice for them is simple: be patient. You need to start 
				from bottom and then improve yourself and grow. Secondly, 
				develop the right attitude and do not be judgemental or react 
				emotionally.<br>
				<br>
				<b>Build a strong foundation<br>
				</b><br>
				Ali Al-Mandhari, Manager-Change Management, Operations 
				Department, BankMuscat believes that his success is a result of 
				unwavering dedication and passion<br>
				<br>
				<b>Can you give us a brief on your educational and professional 
				background?<br>
				</b>I started with BankMuscat in1998 as a trainee, and have 
				worked with the bank for 14 years. While working with the bank, 
				I completed my bachelor’s degree in banking, finance and 
				international trade from Victoria University, Melbourne, 
				Australia. I came back and joined as a senior staff. In 2005 I 
				went again to Melbourne to do my master’s in business and 
				finance with a specialisation in financial risk management. I 
				came back in 2006 and was posted as an assistant manager in the 
				central operations department. In 2008, I moved to my new 
				department which is change management. In this capacity I am 
				involved with process re-engineering in the organisation. In 
				2011 I got promoted as a manager. <br>
				<br>
				<b>You made it to the Top 10 of the NTI BizPro Awards. What has 
				enabled you to do well at a young age ?<br>
				</b>It is the result of my dedication and passion towards my 
				work. Banking runs in my family as my father worked in Grindlays 
				Bank as a manager and he has always been my role model. At 
				BankMuscat, I like the challenge that comes your way on a daily 
				basis. This helps one to learn and grow all the time. You can 
				learn a particular process, but the changes in a system or the 
				regulatory framework keep one on one’s toes. I believe that 
				one’s learning curve should never stop.<br>
				<br>
				<b>We find a number of young Omanis shifting jobs every few 
				years. What has made you work at BankMuscat for 14 years? <br>
				</b>The opportunities and challenges that BankMuscat offers is 
				unique. Our customer base, branch network and market share is 
				the largest in Oman and this ensures that one has a larger 
				canvas to operate on. The culture of the bank and the leadership 
				of the bank is admirable. We look upto the senior management and 
				want to be like them in future. Moreover, the bank facilitates 
				your growth, it encourage us to train ourselves and we have some 
				of the best trainers from around the world coming and educating 
				us about leadership, team work, project management etc. building 
				our expertise.<br>
				<br>
				<b>How do you spend your spare time, are there certain hobbies 
				that you pursue? <br>
				</b>I am into photography and combine it with my other hobby 
				which is trekking. I do a lot of trekking with my colleagues in 
				the bank. Photography is something that I have been passionate 
				about for a while, but to get the right equipment and cameras 
				took some time. I like nature photography and portraits, but the 
				latter is specifically for family members. Trekking is a part of 
				BankMuscat’s staff engagement programme. The programme started 
				in 2011 and we trek under the guidance of Hamad al Harthy, the 
				ace mountaineer who has scaled Kilimanjaro. We trek in different 
				locations, for example, we went to Jebel Akhdar and trekked for 
				around 30 kms over seven hours. We also did Jebel Shams, at 
				times we trek along the Baushar mountains for three to four 
				hours. I also like to read books about technology, photography, 
				leadership and managing teams. <br>
				<br>
				<b>You are a role model for young Omanis, what advice would you 
				give to the youth of the country? <br>
				</b>My advice to youngsters is that they should take it easy and 
				not rush into things. Build a solid foundation, gain knowledge 
				and experience gradually and surely but steadily you will reach 
				where you want to be.<br>
				<br>
				<b>No free lunch in life<br>
				</b><br>
				Mohammed Abdullah Al Shukaili, Manager, Billing &amp; Operations 
				Support, Customer Care – CBU, Omantel has faced numerous 
				challenges in his job which have helped him emerge as a strong 
				leader<br>
				<br>
				<b>Can you talk about your career progression?<br>
				</b>After my high school in Oman, I got a scholarship to Canada 
				where I graduated in software engineering and computer science 
				in 2002. My first work experience was with Omantel as a software 
				programmer in the billing system which is the main part of any 
				telecom company. After six years as a software programmer, I 
				found out an internal vacancy for a manager in the billing and 
				operations department. I applied for that and got it. It was a 
				big move and I soon discovered that there was another part to me 
				which I had never discovered. But actually it was a challenge. 
				Earlier, I did not have a team but now I had to assume a 
				leadership role as I had around 13 people around me. It took 
				time but I managed to handle the responsibility in a worthy 
				manner.<br>
				<br>
				During my years of study in school, I used to be an active 
				member in radio and broadcast group where I had to present the 
				daily programme in front of the students and also among the 
				press reporters, other groups and voluntary workers, and I also 
				did workshops which helped me in building self confidence and 
				creativity skills.<br>
				<br>
				<b>What kind of projects did you handle in Omantel, especially 
				after becoming the manager for billing and operations support 
				unit?</b><br>
				Working in the billing system gave me a rich experience and I 
				worked on various projects like Blackberry services, number 
				portability and was the project technical leader many times. 
				After I became the manager for billing and operations, my first 
				big project was the bill payment machines. This was good for me 
				because it allowed me to interact with vendors, logistics and 
				the installation departments. We have presently 63 of those 
				machines across the country installed within a span of two 
				years.<br>
				<br>
				<b>What is the next step for you?<br>
				</b>The billing and operations is the most vital department and 
				I have been in this for almost nine years. Now, I want to add 
				some marketing skills into my portfolio. Then when I achieve 
				that, I will be able to fill up any leadership shoes because by 
				then I will have experience in all aspects of a telecom company. 
				Just having titles and a nice salary is not enough but your 
				career progression should happen when you have the right 
				experience; then only can you fulfill your responsibilities 
				properly.<br>
				<br>
				<b>Who was your role model?<br>
