						<font face="Verdana" size="4" color="#C00000">
						YOUNG EXECUTIVES TO WATCH OUT FOR</font></b><p class="style15">
				<span class="style14"><strong>NTI BizPro Awards 2012 were 
				presented to young achievers after a rigorous filtering process 
				by KPMG. Mayank Singh and Visvas Paul D Karra report</strong></span><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-june-12-4.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="350" height="349"></font><p align="justify" class="style13" dir="ltr">
				<b>Banish the watch<br>
				</b><br>
				A focussed approach has helped Abbas Hassan Al Lawati, Manager, 
				Corporate Finance and Advisory, BankMuscat to achieve success
				<br>
				at a young age<br>
				<br>
				<b>You were one of the winners of the NTI BizPro Young Achievers 
				Award 2012. What are the values and attributes that have enabled 
				you to win this coveted accolade?<br>
				</b>I have been raised up with the values of commitment, 
				dedication and responsibility. If you look at your watch while 
				working then your career progression will suffer. I believe that 
				there are certain key areas that people should develop in order 
				to grow. The first amongst them is learning. This should be a 
				continuous process. Learning can be either theoretical or 
				practical. I completed my CFA, which strengthened my theoretical 
				knowledge. Simultaneously, I have also enhanced my job skills by 
				observing my seniors and doing soft skill courses. <br>
				<br>
				The second prerequisite for success is to be a people’s person. 
				You may be strong and talented, but if you are bounded by your 
				own bubble, the chances are that your success will be limited as 
				the market is all about interaction and team work. I have worked 
				on improving my inter personal skills. In my family and 
				department they call me Dr Phil, after the famous talk show host 
				Phil McGraw, who guides people to solutions. <br>
				<br>
				Thirdly, if you have been given a responsibility, you should 
				finish it and prove that you can do it regardless of constraints 
				like work pressure and time. I also believe that if a person 
				does not want to achieve anything in life, then his life is 
				useless. You need to challenge yourself and do something that is 
				different from others. Achievements give you personal 
				satisfaction and recognition. I am the second Omani in 
				BankMuscat to do a CFA.<br>
				<br>
				<b>Can you give us a brief on your educational and professional 
				background?</b><br>
				I studied at the American University in Sharjah with a 
				specialisation in accounting and finance. After completing the 
				course I joined BankMuscat in February 2006 as an associate in 
				the corporate finance department. The nature of our job 
				(department) is such that one cannot work on an 8.00am - 3.00pm 
				schedule, one has to be there based on the demands of the 
				assignment on hand. I have learnt this and this has enabled me 
				to shoulder more responsibilities and do well. <br>
				<br>
				You have achieved a lot in a short span of time. What are your 
				future plans and aspirations? <br>
				I want to contribute to the country by transferring my skills, 
				knowledge and experience to the younger generation of Omanis. 
				This can be done through various means like giving lectures, 
				participating in seminars etc. Every young Omani needs to have a 
				good education, commitment, hardwork, and they need to feel that 
				they are working for their own benefit and the betterment of the 
				country. <br>
				<br>
				<b>Are there people who have influenced you or helped you by 
				being your role models? <br>
				</b>It’s important to have a mentor in your mind. If you see 
				your superiors you can learn the traits that have enabled them 
				to reach there. My mother is the person who has built my base in 
				life. On the first day of my job she said, “Don’t look at your 
				watch, once you get into the office just focus on completing 
				your work.” <br>
				<br>
				<b>You are a role model for young Omanis, what advice would you 
				give to the youth of the country? <br>
				</b>Explore what you are good at and once you have figured it 
				out then focus on achieving it with full dedication, commitment 
				and hardwork.<br>
&nbsp;<p align="center" class="style13" dir="ltr">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-june-12-5.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="350" height="349"></font><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Achiever par excellence<br>
				</b><br>
				Suad Al Balushi, Manager, Treasury Support, Treasury and Capital 
				Markets, BankMuscat has a number of firsts to her credit, and is 
				determined to continue her pursuit of excellence<br>
				<br>
				<b>Can you give us a brief on your educational and professional 
				background?<br>
				</b>I started my career in BankMuscat in 2005. I graduated from 
				Sultan Qaboos University (SQU) with a bachelor’s degree in 
				finance with honours. I was also in the dean’s list for three 
				years; you make it to the dean’s list if you get a CGPA of 3.5 
				and above out of four. I joined the treasury department of the 
				bank as officer treasury and started working on getting an ACI 
				certification. I am the first Omani lady-and probably the first 
				lady in the GCC- to complete an ACI certification. I also did an 
				MBA from SQU and graduated last year with distinction and 
				honours.<br>
				<br>
				<b>You made it to the top five of the NTI BizPro Awards 2012, 
				which reflects your success at a young age. What are the 
				attributes and values that have enabled you to do well?<br>
				</b>Learning is something that inspires me. In whatever field 
				that I am in, I want to be the first one who is professionally 
				competent. For example, I had decided that once I graduate I 
				will start working on getting my professional certificate in 
				finance. I am currently pursuing my CFA. I am good at dealing 
				with numbers. While doing my degree course I did my internship 
				at the Central Bank of Oman, where I worked in various 
				departments, and there itself I had decided that if I got a 
				chance to work in a bank, I would like to join its treasury 
				department. When I joined the bank I was not aware of what an 
				ACI certification is but my manager supported me a lot. He told 
				me that since no one else has done this I should get an ACI 
				certificate in the first year itself. Getting an ACI helped me 
				to get recognised at a very early stage of my career in the 
				bank. I have been involved with various initiatives in the bank 
				– for example, I was a member of a the core committee of a new 
				group called BM Minds, which used to organise knowledge quizzes, 
				lectures etc. I am now a part of the BankMuscat IMD initiative. 
				My father has been another big influence in my life. He asks me 
				never to stop learning, and even now he is advising me to do my 
				PhD. Whenever I get a certificate, he is the first person whom I 
				go and show it to. <br>
				<br>
				<b>Having been through the NTI BizPro programme, what are your 
				thoughts on these awards and its role in fostering Omani talent?
				<br>
				</b>This is the first time that I heard about the NTI BizPro 
				Awards. I was not aware of this programme, but once I was 
				nominated by the bank and went through the process I found it to 
				be a tremendous and wonderful initiative. The programme 
				encourages young Omanis to be role model for others. It has also 
				been a good learning process.<br>
				<br>
				<b>How do you spend your spare time, are there certain hobbies 
				that you pursue?<br>
				</b>I am into painting and used to play table tennis. At SQU I 
				won a gold medal in table tennis at the sports day of the 
				College of Commerce and Economics. I have been interested in 
				painting since my school days and have represented my schools in 
				various inter-school painting competitions.<br>
				<br>
				<b>You are a role model for young Omanis, what advice would you 
				give to the youth of the country? <br>
				</b>We are a growing and young country. To build the nation it 
				is essential that we foster education and knowledge. Young 
				Omanis should acquire more professional certificates so that 
				they can compete in the global market.<br>
