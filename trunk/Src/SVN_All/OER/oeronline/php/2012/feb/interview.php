				<br>
				Entrepreneur Of The Year programme<br>
				<b>History:</b><br>
				The Entrepreneur Of The Year® (EOY) awards programme was created 
				in the United States in a single Midwestern city by Ernst &amp; 
				Young in 1986 to honour entrepreneurs whose ingenuity and 
				perseverance have created and sustained successful, growing 
				business ventures. This year EOY is celebrating its 25th 
				anniversary. In those years, the Entrepreneur Of The Year 
				programme has expanded to more than 140 cities in 50 countries 
				with awards presented annually to over 900 of the world’s most 
				successful and innovative entrepreneurs.<br>
				Entrepreneur Of The Year winners include individuals who have 
