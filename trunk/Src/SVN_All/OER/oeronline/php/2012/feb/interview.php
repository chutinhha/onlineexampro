						<span class="style27"><strong>An Elite Club</strong></span><p class="style15">
				<span class="style14"><strong>Rajeev Singh, Partner, Transaction 
				Advisory Services, shares details of Ernst &amp; Young’s 
				Entrepreneur of the Year Award’s Oman edition in an interview 
				with Mayank Singh</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-feb-12-1.jpg" width="300" height="489" style="border: 1px solid #808080; "></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<span class="style14"><strong>
				<br>
				</strong></span><b>Ernst&amp;Young (E&amp;Y) is organising the 
				Entrepreneur of the Year Award (EOY) in Oman for the first time. 
				What are the reasons that convinced you about the Sultanate 
				being ready for an award? </b><br>
				Ernst&amp;Young started EOY, in 1986 and over the years it has been 
				held in 140 cities across 50 countries. Encouraging 
				entrepreneurship is something that is very close to our heart 
				and a part of our CSR policy which works on a triple E concept 
				encompassing entrepreneurship, education and environment. In 
				2007 and 2008 we ran this award collectively for the Middle East 
				and North Africa, in Dubai and Jordan respectively. The MENA 
				Awards ran for two years giving us the confidence about the high 
				level of interest and awareness in these countries. We decided 
				to break it down to a country level and as a result we are 
				organising the EOY Award in the UAE, Oman, Qatar, Jordan, Syria 
				and Palestine in 2012. This will give businessmen from these 
				countries a chance to participate in the World Entrepreneur of 
				the Year Award which is held in Monte Carlo.<br>
				<br>
				<b>What is the process for the selection of winners that is 
				being followed by E&amp;Y Oman? </b><br>
				We launched the EOY programme in Oman on January 7, 2012 and 
				would be receiving applications till January 31. Applicants need 
				to fill up a basic four-page form with some numbers and 
				background details. In the next ten days we will evaluate the 
				applications and on February 12, we will announce a short list 
				of finalists. The finalists will be required to compile a more 
				detailed dossier highlighting their innovative spirit, 
				leadership, value systems, beliefs, integrity and business 
				performance. These are must haves that one would expect in a 
				strong business leader. There are a set of six key criteria that 
				the finalists will be judged against. The finalists get three 
				weeks to compile the information and present it to us after 
				February 12. Once this information is in place by the end of 
				February, a small team from E&amp;Y will visit the finalists for a 
				personal interaction and to do a due diligence on the 
				information presented.<br>
				<br>
				On February 12 we will also disclose the names of a panel of 
				five independent judges. These judges will be judging the final 
				scores for the finalists. The due diligence is important so that 
				the judges are confident about the information presented to 
				them. The judges will do an initial scoring till March 11, 2012. 
				On the morning of March 12, there will be a small interactive 
				session, wherein each finalist will be given half an hour face 
				time with the panel of judges. That half an hour will be broken 
				into two sessions – in the first 15 minutes the finalists will 
				put forward his strengths and anything else that they would like 
				to add while in the next 15 minutes will be set aside for the 
				judges to clarify doubts or to pose any additional queries. Once 
				the judges are through with the process, they will compile their 
				individual scores and pass it confidentially to E&amp;Y, and we will 
				simply compile their scores. In the evening the winners will be 
				declared at a grand event. <br>
				<br>
				<b>Is there an eligibility criteria for business leaders to 
				participate in the EOY awards?<br>
				</b>The eligibility criteria is as follows – an applicant needs 
				to have two to three years of a successful business employing up 
				to ten people. However one need to qualify this by looking at 
				the stature of people who have won the World Entrepreneur of the 
				Year Award in the past and these include stalwarts like Michael 
				Dell, Narayana Murthy etc. We are looking at a winner who is at 
				the same level as these personalities. So more realistically, 
				someone who has been in business for 15 years and has a business 
				of close to $500mn will be comfortable at the World EOY held at 
				Monte Carlo. This is not to deter others, as there is a 
				possibility that there may be someone whose numbers may not be 
				in the same league, but may be very strong in other areas or may 
				have occupied a fantastic niche. <br>
				<br>
				To encourage young entrepreneurs we have a different category 
				called the Emerging Entrepreneur of the Year, which will 
				celebrate the achievements of young entrepreneurs but this award 
				will be given at the Oman level. These are the generation next 
				entrepreneurs who will come up to the level of the EOY in the 
				next five years. The Emerging Entrepreneur of the Year should be 
				someone who has five to seven years of experience behind him and 
				has hit a topline revenue of $100mn in that short span. We are 
				seeing very strong applications at that level. We are not 
				looking at recognising people who are inheritors. <br>
				<br>
				<b>What does a recognition like EOY mean for business leaders 
				and people who win such an award? </b><br>
				Winning a EOY award is a fantastic recognition at a global 
				level. One, the winner gets to go to Monte Carlo for the World 
				EOY function. As the event is spread over two to three days it 
				presents a wonderful opportunity to meet your counterparts from 
				virtually every country in this world. In addition, there are 
				some of our alumni who also participate in the awards as well. 
				To network in an environment where you have undivided access to 
				high-profile business leaders for two to three days is an 
				unparalleled opportunity. Most successful businessmen like to 
				associate themselves with other successful businessmen and if 
				one is able to establish a good rapport then this association 
				continues for a long time. We also organise EOY alumni 
				programmes that keep moving every year. There are three or four 
				such events every year. For example at the ongoing World 
				Economic Forum at Davos, we have an E&amp;Y side event where we get 
				people like Gordon Brown, the former Prime Minister of UK and 
				other high level Economists to come and do a session for our 
				clients and EOY alumni. In 2011 November, we ran a programme at 
				Palms Spring, US where we had a very distinguished panel of 
				speakers talking about the global economic situation for our key 
				global clients and all our EOY alumni were invited to the event. 
				So you have the access to all the E&amp;Y events apart from the 
				bragging rights and branding opportunities.&#8194;<br>
				<br>
				<br>
