						International expertise</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Maroun Mourad, CEO – Middle East General Insurance, 
						Zurich Insurance Company shares his views on Oman’s 
						insurance market, its potential and challenges. An OER 
						report<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/insurance-feb-12-1.jpg" width="200" height="200"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
                		<b>Can you tell us about Zurich Insurance and its 
				operations in the GCC region?</b><br>
				Worldwide Zurich is present in over 170 jurisdictions, and 
				employs over 60,000 people. We entered Oman in January 2011, 
				after Zurich acquired a Beirut based company that operated in 
				Oman, Kuwait and four emirates in the UAE and Lebanon. It’s been 
				a year of integration, fixing and positioning. The company is 
				called Zurich Insurance Middle East and we are headquartered in 
				Dubai with an office in the DIFC, which houses the regional 
				management. Apart from this we have a separate reinsurance 
				business which targets high value complex assets in oil and gas, 
				infrastructure, engineering, financial institutions etc. We have 
				an office in Bahrain and are working on entering Qatar and Saudi 
				Arabia. The growth in these countries can come either through a 
				Greenfield venture, organically or inorganically. <br>
				<br>
				<b>Will Zurich focus on traditional lines of business or will 
				you be targeting niche areas in Oman? </b><br>
				As a business, Zurich operates in a few areas and we do not 
				target customers across segments in our products. Our focus is 
				on the corporate and mid market to high networth individuals. As 
				we are starting out in Oman we cannot afford to be everything to 
				everyone – so we will be offering engineering, property, 
				casualty, financial lines and financial institutions, directors 
				and officers liability insurance. We will also tap into the more 
				traditional lines like personal, accident, motor and medical 
				insurance. In the complex speciality lines the combination of 
				our international expertise and local knowledge, backed up by 
				one of the strongest balance sheets in the industry makes us a 
				formidable player. We enjoy a AA minus from SNP, an A from 
				Moody’s and AM Best. All this put together make us a top five 
				global company in the insurance business. <br>
				<br>
				<b>There is a feeling that Oman’s insurance market has more 
				players than what the size of the market warrants. Given this 
				scenario, the timing of Zurich’s entry into Oman is a bit 
				surprising?</b><br>
				We see Oman as one of the more dynamic and promising markets, 
				while the market may probably have more players than what it 
				needs, this is typical with emerging markets. A metric that is 
				commonly used to measure success in the market is the 
				penetration rate which calculated by multiplying premiums by GDP 
				and this is around one per cent for the region but in Oman it is 
				actually higher. So compared to other markets, insurance 
				penetration in the Sultanate is actually higher. Other emerging 
				markets in Latin America, South Asia or South East Asia stand at 
				under three per cent, so we think that the upside is there in 
				Oman, but it should be driven by more awareness and even 
				compulsory regulation.<br>
				<br>
				<b>You talk about speciality lines of business and niches. Are 
				you going to concentrate on infrastructure, tourism related 
				projects etc? </b><br>
				If you look at the investments taking place, you find that Oman 
				was at the receiving end of significant funds from other Gulf 
				countries. We see Qatar, Bahrain, UAE, Saudi Arabia investing in 
				certain trends like infrastructure, oil and gas, healthcare, 
				tourism and hospitality. So the major projects are in these 
				areas and hence the demand for insurance is also going to come 
				from these sectors. We would also like contribute to job 
				creation in the economy, keeping this in mind we have increased 
				the size of our team in Oman by over 40 per cent in 2011 and 
				will continue to make investments this year. I was at the World 
				Economic Forum recently and there were two things that were 
				being repeated day in and day out – entrepreneurship and job 
				creation. We need to raise awareness, encourage entrepreneurship 
				so that the owner, industrialist etc can focus on their business 
				while we offset the risks of the day-to-day business.&#8194;<br>
