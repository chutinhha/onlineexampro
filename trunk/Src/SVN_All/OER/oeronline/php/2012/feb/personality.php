						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A SMART PERFORMER<br>
						</font><font face="Verdana" size="2">
                		<br>
						Khalid Tawfiq Abdul Rasoul, Director-in-charge of Sharq 
						Sohar Steel Group, shares his views about success, doing 
						business and the development of Oman in an interview 
						with Muhammed Nafie<br>
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<img border="0" src="images/personality-feb-12-1.jpg" align="left" hspace="8" vspace="5"><font face="Verdana" size="1">Does one really need to work hard, or miserably hard, to get 
				where one wants to be, either personally or professionally? Is 
				working hard, juggling multiple projects in a frantic attempt to 
				get them all done, the only way to success? Khalid Tawfeeq Jawad 
				Abdu Rasoul, Director-in-charge of Sharq Sohar Steel Group, 
				replies in negative. Khalid counts smart working rather than 
				hardworking as the secret of his success. He believes that hard 
				working only cannot make everything fall into place. His 
				philosophy of success maintains that a smart worker with clear 
				understanding of his or her strength and skills is more 
				efficient than a hard worker with poor time management. <br>
				<br>
				The way Khalid carefully charted his route to the top of his 
				profession within a short span of time has more to do with a 
				disciplined and meticulous planning than hardworking. You can 
				read out the same expression of optimism on his face as a young 
				and dynamic Khalid talks about his ambition to grow his 
				businesses and make his companies one of the leading groups in 
				the Sultanate in the next ten years. That is why you cannot 
				disbelieve him when he announces his plan to retire in ten years 
				after meeting his objective.<br>
				<br>
				<b>A fruitful career</b><br>
				Khalid started his career as a trade marketing executive with 
				Enhance (Matrah Cold Stores ) after completing his graduation 
				from the University of Missouri–St. Louis in the US in marketing 
				and a master’s in business administration from the University of 
				Hull. The five years in Enhance (Matrah Cold Stores) where he 
				had looked after different divisions including British and 
				American Tobacco and consumers’ and retail divisions were 
				instrumental for Khalid to hone his skills before joining the 
				family business, Sharq Sohar Steel Group, in 2007. Khalid 
				started as a business development manager with Azaiba Furnitre 
				Factory and Sohar Steel Rolling Mill and was promoted as 
				assistant general manager after one year. After the death of his 
				father in 2008, Khalid became the director-in-charge of Sharq 
				Sohar group of companies which owns Sharq Sohar Steel Rolling 
				Mill, Soahr Steel, and Middle East Conversion Industries.<br>
				<br>
				Within a short span of four years Khalid played a pioneering 
				role in the development of Sharq Sohar Steel Group and Aziaba 
				Furniture Factory by introducing innovative changes such as 
				advanced machinery, and expanding their production and export 
				capabilities significantly. His role was also instrumental in 
				making the company a well-known brand. The Omanisation has shot 
				up from 35 per cent to 42 per cent. “We brought about a lot of 
				changes and created new values for our companies. The advanced 
				machinery we introduced helps us save both money and time. We 
				took new land in Sohar and are all set to move Aziaba 
				Furniture’s factory to Sohar by the end of 2013. The strategic 
				location in Sohar would be greatly helpful for the company as it 
				concentrate on exporting to India and Qatar,” avers Khalid.<br>
				<br>
				Khalid also informed that both Sharq Sohar Steel Rolling Mill 
				and Sohar Steel are undergoing a revamp aimed at increasing the 
				total production. Marking a quantum leap forward, the total 
				production capacity of Sharq Sohar Steel Rolling Mill and Sohar 
				Steel will go up from 250,000 tonnes per year to 800,000 tonnes 
				per year. Explaining the company’s plans for the next five 
				years, Khalid exuded confidence that the group is looking to go 
				IPO in the next five years as a holding company. “My dream is to 
				make our group one of the leading business groups in the 
				Sultanate. Now we would like to concentrate more on our existing 
				companies rather than expanding to new areas. However, we are 
				looking to expand our exports to foreign countries. Right now 
				almost 95 per cent of our products target the Omani market,” he 
				avers.<br>
				<br>
				<b>Focus on people</b> <br>
				Khalid accords more importance to taking the employees to trust 
				and winning their confidence. “I believe that our people are our 
				asset. Therefore I would like to work for the staff. My father 
				always used to tell me to work for the workers and not for the 
				company. He intended that working for the workers means working 
				for the company. We have to focus on people. ” Khalid says he is 
				very particular about selecting the best people to his team so 
				that he can build a team of skilled and efficient people who are 
				more responsible to their work. Sharing his vision for Oman’s 
				development Khalid says the country has great potential to grow 
				as one of the most investment friendly areas in the region. But 
				he believes that it has a long way to go in terms of 
				infrastructure development such as railways, roads and 
				development of new tourism attractions. “Recently, we have seen 
				that the opening of Opera House has turned out to be a big boost 
				to the tourism industry, evident from the increase in hotel 
				occupancy in the country after that,” says Khalid. He is all 
				praise for His Majesty Sultan Qaboos bin Said’s recent decision 
				to empower Shura Council which he says has to play a significant 
				role in the development of the country and the welfare of its 
				people.<br>
				<br>
				A passionate sightseer, Khalid has travelled extensively 
				visiting a number of countries such as Australia, Malaysia, 
				Singapore, Thailand, Netherlands, UK, Germany etc. and has a 
				list of countries he wants to visit soon. Khalid has also an 
				uncanny knack for trying and enjoying the various cuisines of 
				whatever places he visits. He has lost his heart to the Chinese 
				food and very often goes to the Chinese Mood Restaurant at Al 
				Bustan Palace. A member of Entrepreneurs Organisation (EO) and 
				Oman Chamber of Commerce Industry (OCCI)’s industrial &amp; 
				promotion committee, Khalid aspires to dedicate more time for 
				social services in the future. His special interest for charity 
				activities reflects in his company’s philanthropic gesture to 
				spend around three per cent of its net profit every year for 
				poor people in the interiors.<br>
