				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<b>The new identity of OER and design changes explain the 
				magazine’s brand ethos<br>
				</b><br>
				The template for OER has been completely revamped to embody the 
				values of the new identity of Oman Economic Review.<br>
				<br>
				Rechristened OER, the new identity seeks to strike a fresher and 
				more contemporary tone in all its manifestations. Opening with a 
				new composition Cover Page, the template is fixed to ensure that 
				the Brand Mark gets all the visibility and import it deserves. 
				No visual will disturb the brand mark panel header.<br>
				<br>
				The Masthead page opens with a new graphic mnemonic each month 
				to hint at the flavour of what the editor wants to draw 
				attention to that month.<br>
				<br>
				The Contents page uses a centrally placed spine which takes a 
				graphic cue from a graduated time line and the various featured 
				articles appear as call outs from this spine.<br>
				<br>
				The reader will note that each page space that the identity 
				lends its mark to, echoes a newer balance of vibrancy and colour 
				and a slightly edgier styling format. <br>
				<br>
				Be it the mixed weight Page titles in both the brand colours or 
				the kinked separator line that heads or bounds each page, there 
				are a collection of several elements that have been carefully 
				derived from the Brand Mark and its familial graphic palette to 
				deliver a consistent “feel”.<br>
				<br>
				Each section carries its own section title on every page and 
				these are bound by deco parantheses and a line to complement the 
				OER brand echo on every facing page.<br>
				<br>
				Without being too heavy, the pages format and styling follows on 
				from the Cover Page style. <br>
				A defined header panel consistently divides content and issue/ 
				date markers etc.<br>
				<br>
				Some other distinctive style formats include usage of full bleed 
				solid colour pages juxtaposed with full bleed visuals which make 
				for a striking and bold opening to any of the sections. The 
				introduction of a transparent copy carrying panel easily draws 
				attention to all the important opening passages.<br>
