		‘We have also entered into direct meetings with various entities, 
		amongst them Spice Communications,’ Omran said, though no agreements 
		have been made.<br>
		<br>
		The second-largest Arab telecom company by market value is also looking 
		at bidding for an Indian telecom licence, he said.<br>
		</font><font face="Verdana" size="2" color="#CC3300"><b><br>
		Gulf projects cross $2 trillion mark </b></font>
		<font face="Verdana" size="1"><br>
		A staggering $2 trillion worth of projects across the Gulf region has 
		been recorded by MEED Projects, a service that provides comprehensive 
		information on industry updates in the Gulf. The UAE accounts for 37 per 
		cent of the total project value reaffirming its position as the biggest 
		projects market in the Gulf.<br>
		<br>
		<br>
		<br>
		<b>
		<img border="0" src="gcc_dairy10.jpg" align="right" hspace="4" vspace="4" width="250" height="201"></b>The 
		MEED Projects Index has increased in value by 40 per cent ver the last 
		12 months.<br>
		The UAE recorded the highest growth, with total project values in the 
		federation rising by 46 per cent over the past 12 months. Kuwait, it was 
		found, had the highest proportion of unawarded contracts, representing 
		over 90 per cent of all projects planned. <br>
		<br>
		Construction still continues to dominate project development, with major 
		new real estate projects being – Sudair Industrial City in Saudi Arabia 
		at $40 billion, Masdar City, Abu Dhabi – $22 billion, Dilmunia mixed use 
		development in Bahrain worth $4.2 billion and Limitless A-Wasl 
		development in Riyah valued at $12 billion. This leaves the MEED 
		Projects value of investment in the Gulf by sector standing at – 
		Construction – $1,207 billion; Industry – $70 billion; Oil and Gas - 
		$430 billion and Water and Waste - $40 billion.<br>
		</font><font face="Verdana" size="2" color="#CC3300"><b><br>
		</b></font><font face="Verdana" size="1"><b>
		<img border="0" src="gcc_dairy11.jpg" align="left" hspace="4" vspace="4" width="150" height="93"></b></font><font face="Verdana" size="2" color="#CC3300"><b>UAE 
		committed to fixed exchange rate for now</b></font><font face="Verdana" size="1"><br>
		The UAE will not drop its currency’s peg to the US dollar “for now,” 
		said His Highness Shaikh Mohammad Bin Rashid Al Maktoum, Vice-President 
		and Prime Minister of the UAE and Ruler of Dubai, while confirming the 
		issue was being studied by a committee.<br>
		“Up to now we are still with the dollar. Dropping the dollar peg is not 
		easy. However, a committee is studying the benefits of staying with the 
		peg or not,” Shaikh Mohammad. <br>
		On the impact of the ongoing US mortgage crisis on the UAE economy, 
		Shaikh Mohammad ruled out any likely adverse effect on UAE’s property 
		market adding, “It will not affect us. We have made arrangements to 
		enable our property market to avoid any such negative impacts.”</font></p>
