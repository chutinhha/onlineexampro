		<img border="0" src="gcc_dairy8.jpg" align="left" hspace="4" vspace="4" width="150" height="255"></b>Emirates 
		Telecommunications Corporation (Etisalat) is currently in talks with 
		India’s Spice Communications and others about making possible 
		investments in the Indian telecom market<br>
		‘India is a very attractive market for Etisalat and we are studying 
		different market entry strategies to determine the most appropriate 
