        Slated to be the country’s biggest shopping mall, Bahrain City Centre 
        will stretch over three levels and occupy a built-up area of 150,000 
        square metres. It will also have two international hotels managed by 
        Kempinski, with 550 luxurious rooms offering four- and five-star 
        facilities. “Built at an investment of over 25 million Bahraini dinars, 
        the Kingdom’s biggest multi-use development is expected to contribute 
        significantly to the local economy,” MAF Shopping Malls, the retail 
        development subsidiary of Majid Al Futtaim Group, said.<br>
        Among the host of attractions it will offer are the biggest 
        indoor/outdoor climate controlled water park in the Middle East; Cineco 
        Cinema, the largest cinema complex in the region with 20 screens; 
        international and exclusive brands. It will also have a 16,600 square 
        metre Carrefour hypermarket.<br>
