        government to the newly-established Bahrain Airport Company (BAC). The 
        move, said BAC chairman Shaikh Daij bin Salman Al Khalifa is in line 
        with government initiatives to enhance the airport’s services and its 
        infrastructure. The agreement provides licensing for the current airport 
        grounds in addition to adjacent lands earmarked for the future expansion 
        of the airport. BAC Chief Executive Officer Dr Osama Al Ali said that 
        the company will direct its efforts at attracting new airline through 
        the execution of several projects. Some of them being, expansion of the 
        current airport and renovation of existing infrastructure, construction 
        of new building for the airport and implementation of projects to 
        diversify business activities connected with the aviation industry. <br>
