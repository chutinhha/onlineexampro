		diversify its economy</b></font><font face="Verdana" size="1"><br>
		Kuwait is gearing up for the post oil era and plans to diversify its 
		economy beyond oil. The Gulf State intends to attract more investment, 
		speed up the sale of state firms and ease land ownership rules.<br>
		A 2009-2014 policy strategy policy outlines plans for controlling 
		spending amid rising inflation in the next five years. The Gulf state 
		wants to emulate the success of neighbours Dubai and Bahrain, which have 
		become regional financial centres and popular tourist destinations, but 
		setting up a non-oil economy is still in the early stages.<br>
