        International food giant Kraft Foods has set up its all-new, 
        state-of-the-art manufacturing plant at the Bahrain International 
        Investment Park (BIIP). Kraft Foods Bahrain WLL began construction of 
        the $40 million facility and the plant is expected to pump $120 million 
        per year in wages, raw and packaging material and operations. The 60,000 
        square-meter factory began production late last year for Kraft Foods’ 
        cheese and powdered beverage products, including flagship brands Tang, 
        Kraft Cream Cheese Spread and Kraft Cheddar Cheese. It will supply 
        products for all Middle East and Africa markets, which is estimated to 
        be around 60,000 tonnes per year. <br>
