		Abu Dhabi International Airport is set to open its third terminal this 
		summer that will be exclusively used by national carrier Etihad and 
		boost the airport’s passenger handling capacity by 70 per cent.<br>
		<br>
		The UAE capital is expanding its airport capacity at a cost of $6.8 
		billion as part of its efforts to become a major tourist hub.<br>
		In another three years, the Midfield Terminal Complex will increase the 
		annual passenger handling capacity at the airport to 20 million, which 
		can be doubled later.<br>
		<br>
		With the opening of Terminal 3, Abu Dhabi will add 5,000 square metres 
		of airport retail space to the existing 5,000 square metres. The two 
		terminals are operating close to their capacity of seven million 
		passengers a year. The growing Etihad route network is providing a boost 
		to passenger traffic.<br>
