		Qatar to set up Mauritania airline </b></font>
		<font face="Verdana" size="1"><br>
		The Emir of Qatar, Sheikh Hamad bin Khalifa al-Thani, signed a deal to 
		found a new airline in Mauritania during a visit to the West African 
		nation.<br>
		The airline, whose name has not yet been announced, will be 60 percent 
		controlled by Qatar’s Al Bateel Group, 30 percent by Mauritanian 
		investors and 10 percent by the Mauritanian state. <br>
		“The fleet of the new airline will be five planes: three Boeing 737 and 
		two small planes for interior and regional flights,” said Ahmed Ould 
		Lefghih, secretary-general of the Mauritanian transport ministry.<br>
