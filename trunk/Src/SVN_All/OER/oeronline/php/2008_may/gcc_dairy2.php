        <br>
        Middle East central bankers ‘unshaken’ by dollar decline</font></b><font face="Verdana" size="1"><b><br>
		</b>Central bankers in the Middle East are proving the US dollar’s decline 
        to record lows is a small price to pay for the loyalty and oil money of 
        their biggest Western ally. Saudi Arabia keeps the riyal fixed at 3.75 
        to the dollar by purchasing or selling the greenback with the local 
        currency. <br>
        <br>
        <b><img border="0" src="gcc_dairy2.jpg" align="right" hspace="7" vspace="7" width="250" height="157"></b>None of the Gulf countries indicate they will follow Kuwait’s example 
        and de-link from the dollar. According to Simon Williams, the chief Gulf 
        Economist at HSBC Holdings Plc in Dubai, doing so would grossly undercut 
        these countries’ $500 billion worth of assets denominated in the dollar. 
        Much of the GCC countries’ revenue which comes from oil is priced in the 
        dollar, hence their unwillingness to decouple from the US currency. For 
        instance Oman earned about 65 per cent of its revenue from oil in the 
        past year, government data show. Half of Saudi Arabia’s gross domestic 
        product is accounted for by the oil and gas industries.<br>
		<br>
		</font><b><font face="Verdana" size="2" color="#CC3300">Emaar, DP World 
        in Saudi port deal</font></b><font face="Verdana" size="1"><br>
        <b>
		<img border="0" src="gcc_dairy5.jpg" align="left" hspace="4" vspace="4" width="150" height="112"></b>Saudi developer Emaar Economic City and Dubai’s DP World have entered 
        into an initial agreement to develop and operate the Red Sea’s largest 
        port at King Abdullah Economic City. ‘The seaport will be the largest in 
        the Red Sea and one of the top 10 ports in the world with a capacity to 
        handle 20 million TEUs (twenty foot equivalent container units),’ Emaar 
        said in a statement.<br>
        <br>
        ‘A multi-purpose cargo terminal is scheduled to be operational by end of 
        2010 and a 1.6 million TEU container terminal by mid-2011 after which 
        the capacity of the port will be increased on several phases,’ it 
        stated. Emaar Economic City, an affiliate of Dubai-based Emaar 
        Properties, is spearheading the development of the $26.7 billion 
        project.<br>
        <br>
		</font><b><font face="Verdana" size="2" color="#CC3300">Saudi Arabia 
        cuts import tariffs to curb inflation</font></b><font face="Verdana" size="1"><br>
        Saudi Arabia has decided to cut import tariffs on food and building 
        materials following a two-fold increase in inflation during the last six 
        months. Duties on food products such as frozen poultry, dairy products 
        and vegetable oil have been reduced to five per cent from 20 per cent 
        and these form part of latest measures by the government to counter the 
        impact of inflation which touched a 27-year high of 8.7 in February. 
        Levies on building materials such as paints, gypsum, electrical cables 
        and plastic pipes will also be brought down to five per cent, while 
        duties on wheat products will be eliminated entirely.<br>
