        </font><b><font face="Verdana" size="4">Tender, Loving Caranes</font></b><font face="Verdana" size="1"><br>
		<b>We take the newest MPV from KIA, the Carens, for a spin around the 
		city<br>
		</b><br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="auto_talk1.jpg" width="200" height="231" align="right" hspace="6" vspace="6"></font></b><font face="Verdana" size="1">KIA was founded in 1944 and as of today is the second largest automobile 
		manufacturer in Korea. The Carens, a compact MPV, was launched in 1999. 
		The present Carens was launched back in late 2006, as a 2007 model, and 
		continues unchanged for 2008.<br>
		<br>
		Carens is made available as a seven-seater with two trim levels. The one 
		thing you’d notice within the Carens is the abundance of head and leg 
		room. There is a lot of storage space available and both the back rows 
		fold flat. <br>
		In basic trim the Carens comes with standard features like power windows 
		and mirrors, cassette player and rear AC vents. The interior plastic is 
		tough and the build quality is noteworthy. All buttons are ergonomically 
		placed and instruments easy to read. In the safety department, Carens 
		has made no compromises. It scored a four star at the Euro NCAP.<br>
		<br>
		<b>Drive</b><br>
		The driving position will fit almost any driver, thanks in part to the 
		ample seat adjustments available. Although not always necessary, a reach 
		adjustment for the steering column would have made it near perfect. 
		Driving dynamics is excellent and the ride quality seems to improve with 
		speed. Forward visibility is good, while a balanced chassis and 
		well-weighted steering provide excellent control.<br>
		<br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="auto_talk2.jpg" align="left" hspace="6" vspace="6" width="150" height="239"></font></b><font face="Verdana" size="1">All Carens in Oman come equipped with a 143hp 2.0l CVVT inline-four, not 
		immensely powerful but more than adequate for trotting around the city 
		and for long drives. This engine is complemented by either a five-speed 
		manual or a four-speed automatic. We did not get a chance to test the 
		automatic but the manual did its job well. Gear changes are smooth, 
		thanks in part to the light clutch. Even the ratios suit the engine 
		quite well, making it a very user friendly car. Engine noise does 
		penetrate into the cabin, but the overall smoothness of the engine more 
		than compensates for it. KIA has done a decent job of eliminating road 
		and wind noise from the interior.<br>
		<br>
		<b>Verdict</b><br>
		If you’re in the market for a spacious family MPV that offers reliable 
		performance and good value for money, this one will certainly live up to 
		your expectations. At RO5,899, it undercuts almost all its rivals with 
		its price making it one of the most practical alternatives to a large 
		SUV.<br>
		<br>
		<b>Specifications for Kia Carens</b><br>
		Engine Type: Inline-four DOHC CVVT EngineDisplacement: 1,998cc<br>
		Length x width x height (mm): 4,545 x 1,800 x 1,720
		Power (SAE): 143hp@6,000rpm<br>
		Specific Output: 71.5hp/litre<br>
		Torque: 189Nm@4,250rpm<br>
		Transmission: 5-Speed Manual or 4-Speed Auto<br>
		Fuel Consumption: 7.0 litres/100km<br>
		Fuel Tank capacity: 50litres<br>
		0-100kmph: 11.0sec</font></p>

