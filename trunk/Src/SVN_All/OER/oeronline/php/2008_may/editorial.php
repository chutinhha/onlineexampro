</font><b><font face="Verdana" size="4">Oman, a sleeping nation?</font></b><font face="Verdana" size="1"><br>
<br>
If the Sultanate of Oman were a dormant nation as it is made out to be, how can 
one account for the rising rentals of residential blocks, the serpentine lines 
of automobiles at road signals and the thronging crowds at the malls? What about 
the unending caravans of long haul trucks plying on Omani roads carrying heavy 
duty equipment and finished products in every direction?<br>
<br>
Today, Oman is in overdrive. Its GDP has shot up to US$40.29 billion, with the 
non-oil sector contributing US$22.7 billion to the nation’s kitty. More than 
half of the GDP. Truly remarkable, especially as the popular notion is that the 
growth has mainly been fuelled by high oil prices. A 7.2 per cent growth in real 
terms, the second fastest pace in the current decade. This is cause for 
celebration. Sleeping nations don’t post such high growth.<br>
<br>
The Minister of National Economy, His Excellency Ahmad BinAbdul Nabi Macki, is 
not joking or being humorous when he says that the country has appointed 
consultants to carry out a feasibility study for a railway network in Batinah 
region. The idea is to connect Sohar, the new industrial city situated in the 
Batinah region to Barka in the north of Muscat. The proposed 200-km rail link 
for speedy cargo movement will be extended when Duqm gets ready in the next five 
years, if not earlier. The objective is to ensure easier and speedier movement 
of cargo. A nation that is in limbo does not need tracks to move goods.<br>
<br>
Macki recently undertook a three-nation tour of Vietnam, South Korea and Japan. 
The objective: To boost economic cooperation and trade, as also explore the 
possibility of setting up joint ventures in tourism, trade, industry, oil and 
gas, ports and shipping. Oman is eager to step out and help other nations and 
earn goodwill besides money. Oman Oil may cooperate with Petro Vietnam to build 
storage and distribution systems in Vietnam, supplying technical services, 
trading in products and establishing joint ventures to invest in a third 
country. Those who sleep don’t step out of their doors, leave alone shores!<br>
<br>
It is not that the Sultanate’s government alone is seeking investment 
opportunities and boosting trade. Other nations too realise the increasing role 
that Oman has come to play in the region and the global economic scenario. For 
example, during the recent visit of His Highness Sayyid Haitham bin Tariq Al 
Said, Minister of Heritage and Culture to Japan, the two countries affirmed the 
importance of facilitating cooperation in the field of human resources and 
promoting the industrial sector in the Sultanate.<br>
<br>
Oman is targeting a production of 70 MCMD of natural gas this year, according to 
Dr Zayed bin Khamis al Siyabi, Director-General of the Ministry of Oil and Gas. 
While Petroleum Development Oman is investing over US$1 billion in various 
projects to enhance output, British Gas and British Petroleum will be pumping in 
an equal amount over the next 3-5 years for developing gas blocks.<br>
<br>
Does all this sound like the activities of a sleeping nation? Absolutely not.<br>
