</font><b><font face="Verdana" size="4">Renaissance announces strategic 
divestment of technology group<br>
</font></b><font face="Verdana" size="1"><br>
<b>Renaissance’s divestment of IMTAC has been described as an ‘excellent 
outcome’ for all stakeholders. The strategic fit establishes a powerful IT 
coalition with enormous potential throughout the region</b></font><b><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1"><br>
<b>
<img border="0" src="in_news1.jpg" width="300" height="230" align="right" hspace="4" vspace="4"></b>Renaissance Services SAOG recently announced its acceptance of a definitive, 
binding offer from the Oman based Services &amp; Trade/Sobha Group, to acquire the 
Technology Group business – IMTAC and its subsidiaries – for approximately $41 
million. The transaction realises a pre-tax capital gain of approximately RO6 
million (US$16 million), and is effective, in retrospect, from January 1, 2008, 
with the sale based on the audited balance sheet of December 31, 2007.<br>
<br>
This divestment is in line with Renaissance’s stated strategic intent to focus 
on its core oil and gas Services businesses; and confirms earlier announcements 
that the company was considering divestment of its successful Technology, Media, 
and Training businesses.<br>
<br>
Renaissance CEO, Stephen Thomas, stated that the transaction was “an excellent 
outcome for all stakeholders: As Oman’s leading IT company, Imtac remains in 
Omani ownership, with the added benefit of a great strategic fit with the India 
based IT businesses of the Services &amp; Trade /Sobha Group, including SRIT. This 
puts in place a powerful IT coalition on an Oman/India axis with enormous 
potential throughout the region”.<br>
<br>
Imtac CEO Ashok Sardiwal explained that “SRIT is an SEI CMM L5 enterprise with a 
world class suite of software products in Healthcare, Telecom &amp; E-services, 
which are entirely complementary with IMTAC’s market leadership in Oman, our 
acknowledged technology services delivery competence and regional presence in 
those domains”. <br>
<br>
The partnership of Brigadier-General (Retd.) Suleiman bin Mohammed Al-Adawi and 
P.N.C Menon has created a multi-billion dollar international conglomerate over 
the past decades. Aside of construction and property development they have very 
successfully built an international technology enterprise. A joint statement 
quoted them thus, “The entry of IMTAC into our group allows us to target market 
leadership in the booming MEA region from Oman. We have always strongly believed 
in the vision of His Majesty Sultan Qaboos in terms of striving for global 
quality and service delivery benchmarks, without losing focus on our Omani 
heritage, culture and social commitment. IMTAC, its people, its customers and 
its positioning in both Oman and the region represents an institution of 
excellence through which we will deliver our regional aspirations.”<br>
<br>
Renaissance Chairman, Samir Fancy, expressed the company’s admiration for the 
new owners saying that “Renaissance has retained a token minority stake in Imtac 
and we will play our full part in continuing to grow the IT space nationally and 
supporting IMTAC’s international ambitions. It was always important to us that 
this divestment be in the best interests of IMTAC management, employees and 
customers, and this new ownership structure is ideal for stable business 
continuity as well as exciting new potential”.<br>
<br>
Indian Ambassador to Oman, HE Anil Wadhwa, expressed his pleasure at the deal 
and emphasised the potential of collaboration in the IT space between two first 
class Omani and Indian enterprises, “There is a long history of success in 
commercial synergy between our two countries. In Information Technology, India 
has built up valuable brand equity in the global markets. India’s most prized 
resource in today’s knowledge economy is its infrastructure and readily 
available technical work force. These attributes can only bring further 
opportunity to build on Imtac’s achievements in Oman and the region. I wish them 
every success”.<br>
<br>
<b>Renaissance Services SAOG is an international services company listed on the 
Muscat Securities Market in the Sultanate of Oman, with a primary focus on 
providing safe, quality, efficient services to the oil and gas industry. 
Renaissance owns and operates a combined offshore support vessel fleet 
comprising 61 vessels with another 11 vessels currently under construction. 
Renaissance has engineering businesses in oil &amp; gas fabrication and afloat ship 
repair; and is a leading turnkey contract services provider, providing 
facilities management, facilities establishment, contract catering, operations &amp; 
maintenance services. Renaissance employs over 10,000 people operating in over 
16 countries. <br>
