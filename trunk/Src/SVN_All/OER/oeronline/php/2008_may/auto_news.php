		international prize for City Safety</font></b><font face="Verdana" size="1"><br>
		Volvo Cars’ in-house developed technology for avoiding low-speed 
		collisions received the American ‘Traffic Safety Achievement Award’ at 
		this year’s World Traffic Safety Symposium in New York. Volvo’s safety 
		feature, City Safety, made a huge impression on the jury members owing 
		to its ability to prevent or lessen the severity of collisions at low 
		speeds, thus reducing the risk of personal injuries and damage to 
		vehicles. Volvo will be the world’s first to install City Safety as 
		standard equipment in a vehicle. If the vehicle in front brakes suddenly 
		and City Safety assesses that a collision is imminent, the brakes are 
		prepared for action. If the driver does not take action, the car is 
		braked automatically. More details on the Volvo range of cars can be 
