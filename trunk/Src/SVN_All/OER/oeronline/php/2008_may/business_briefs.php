</font><font size="2" color="#CC3300" face="Verdana"><b>Vision Insurance enters 
Oman</b></font><font face="Verdana" size="1"><br>
<img border="0" src="2.jpg" width="250" height="167" hspace="4" vspace="4" align="left">Vision Investment Company marked its entry into Oman’s insurance sector with the 
launch of Vision Insurance. The company has simultaneously launched operations 
at Al-Ghubra, Sohar and Nizwa. As a composite insurance company, Vision is 
licensed to offer both life and non-life insurances. Vision Insurance was set up 
by Vision Investment Services in association with Al Wathba National Insurance 
Company, UAE. The other Omani promoters of Vision Insurance are Sheikh Salim 
Said Hamed Al Fannah Al Araimi, founder and Chairman of Galfar group of 
companies, Tawoos LLC, founder of Renaissance Services SAOG, A’ Sharqiya 
Investment Holding Company SAOG, Maqbool Ali Salman and Hassan Ali Salman 
founders of Al Hassan Engineering Group. It has a capital base of RO5 million.<br>
