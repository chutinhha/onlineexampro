<font size="2" color="#CC3300" face="Verdana"><b>The Wave, Muscat to deliver 120 
homes this year</b></font> <font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>
<img border="0" src="busness_brif.jpg" width="250" height="167" align="right" hspace="4" vspace="4"></b></font><font face="Verdana" size="1">The Sultanate’s fully master-planned community project, The Wave, Muscat is 
expected to deliver over 50 villas and apartments this summer, according to 
company officials. With the project now entering stage 5 of the construction 
phase, around 120 homes will be ready for delivery by the end of this year.<br>
“With contracts worth RO200 million in place, over 300 residential foundations 
built, 200 rooftop stages completed, 2,500 men working on site and 
infrastructure well underway. The Wave, Muscat is on track to welcome its first 
residents,” said Amer Mustafa al Fadhil, Vice President, External Affairs.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>PSC-led JV plans free 
zone at Port Khasab </b></font><font face="Verdana" size="1"><br>
Following the grant of provisional government approval to a PSC-led joint 
venture to operate and manage Port Khasab in Musandam Governorate, the JV is 
contemplating developing a free zone in the vicinity of the port. According to 
PSC Chief Executive Officer, Saud Bin Ahmed Al Nahari, the move forms part of 
efforts by the Corporation to explore new business opportunities and diversify 
its operations beyond operating and managing Port Sultan Qaboos in Muscat. Last 
year, PSC announced it had entered into an MoU for the establishment of a JV 
with German-based I Q Martrade Holding, which specialises in port operations and 
ocean freight. PSC then disclosed that the JV was granted a concession award in 
principle to develop, operate and manage Port Khasab.<br>
