</font><font size="2" color="#CC3300" face="Verdana"><b>Sohar Aluminium well on 
track</b></font><font face="Verdana" size="1"><br>
Sohar Aluminium is all set to meet its mid-June target for its first hot metal 
production following a string of success in the company’s Carbon Department in 
March. The department produced its first ‘good green anode’ on March 26, marking 
the first step in the on-going metal production process. “From the production of 
the first anode, less than a week of process tuning and equipment adjustments 
were necessary to produce the quality of ‘good green’ anode that will suit our 
requirements,” said General Manager Operations, Michel Huot. Another milestone 
was achieved three days later when the smelter’s new Anode Baking Furnace was 
successfully started, along with the Solios Fume Treatment Centre and the 
Innovatherm’s Firing System. The Sohar Aluminium smelter is in the process of 
being handed over by the company’s EPCM contractor, Bechtel. To date, the 
smelter construction is around 85 per cent complete and commissioning in most 
areas is well underway.<br>
