</font><font size="2" color="#CC3300" face="Verdana"><b>Al Anwar plans 
investment bank in Saudi Arabia</b></font><font face="Verdana" size="1"><br>
Al Anwar Holdings, a leading investment holding firm in Oman, plans to set up an 
investment bank in Saudi Arabia with regional investors. The partners in the 
venture include Bahrain-based Addax Bank and a Kuwaiti investment firm, among 
others. Al Anwar holdings will hold 25 per cent stake in Addax Securities, which 
will focus on financial services, while the three main partners will have 25 per 
cent holding each and the remaining 25 per cent will be allotted to other 
partners in the joint venture. The new firm is expected to commence operations 
within three to four months, according to officials from Al Anwar Holdings. This 
latest move is a part of Al Anwar Holding’s overall strategy to expand and 
explore growth opportunities in regional and Asian markets. Last year, for 
instance, the company acquired 14.99 per cent stake in Almondz Global Securities 
Limited, an Indian financial services company.<br>
