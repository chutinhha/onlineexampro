</font><font size="2" color="#CC3300" face="Verdana"><b>Govt targets 70 million 
cubic metres of gas per day </b></font><font face="Verdana" size="1"><br>
<img border="0" src="3.jpg" hspace="4" vspace="4" align="right" width="250" height="167">The Sultanate has set a production target of 70 million cubic metres of natural 
gas per day this year as against last year’s 63 million cubic metres per day, 
according to Dr Zayed bin Khamis al Siyabi, Director General (production and 
exploration) of the Ministry of Oil and Gas. He added the commissioning of the 
Kauther gas treatment plant in central Oman helped Petroleum Development Oman 
enhance its gas output. “PDO is investing over US$1 billion in various projects 
to enhance output this year and the years to come,” said Dr Al Siyabi. He also 
pointed out that two British oil and gas firms – British Gas and British 
Petroleum – will be investing nearly US$1 billion over the next three to five 
years towards the development of some of Oman’s largest gas and condensate 
concessions. These include the Khazzan and Makarem gas fields in central Oman, 
with estimated reserves of 30 trillion cubic feet, and the Abu Butabul gas and 
condensate discovery with reserves of 8 trillion cubic feet.<br>
