</font><font size="2" color="#CC3300" face="Verdana"><b>Next sale of Blue City 
homes in June</b></font><font face="Verdana" size="1"><br>
Close on the heels of the rapid sell-out of earlier sales releases comes the 
next set of much-anticipated releases of Al Madina A’Zarqa. This next release 
will include some of the Gulf’s most unique and prestigious properties – Foster 
+ Partners designed sea view residences and a selection of golf course villas. A 
unique 32 sq. kilometre project, Al Madina A’Zarqa is busy sculpting an entirely 
new city of more than 200,000 people and represents an estimated US$15-20 
billion total investment. The first phase of the project will accommodate 27,000 
residents as well as local, regional and international visitors and is primarily 
a residential and tourism development with leisure and retail components. Said 
Anees Issa Al Zadjali, Chairman of the Board at Blue City Company 1, “The design 
of the project is currently well underway and on schedule; the first residential 
units will commence delivery in 2010 and Phase 1 will be completed by the end of 
2012.”<br>
