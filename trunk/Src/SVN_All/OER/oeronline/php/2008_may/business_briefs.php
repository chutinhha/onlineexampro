<img border="0" src="4.jpg" width="167" height="200" align="left" hspace="4" vspace="4"></font><font size="2" color="#CC3300" face="Verdana"><b>Bharat Oman Refineries 
plans $1bn IPO</b></font><font face="Verdana" size="1"><br>
Bharat Oman Refineries (BORL) is expected to raise US$1 billion through its 
initial public offering towards setting up a refinery project in the Indian 
State of Madhya Pradesh. The company has already filed a draft red herring 
prospectus to this effect with the Securities and Exchange Board of India. BORL 
is promoted by Bharat Petroleum Corporation Limited and Oman Oil Company and has 
interests in oil refining and marketing of petroleum products. The upcoming 
refinery is a grassroots project to be set up at a capital cost of around US$2.5 
billion, which will be funded through a mix of debt and equity. BORL has entered 
into an agreement with a consortium of lenders for a debt component of 
approximately Rs 6,400 crore. It is expected to commence commercial operations 
by January 2010. BORL has signed an off-take agreement with BPCL according to 
which the latter will purchase most of the refinery’s petroleum products.<br>
