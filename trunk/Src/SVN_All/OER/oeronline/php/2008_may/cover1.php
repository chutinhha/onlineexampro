<p class="MsoBodyText"><b><font face="Verdana" size="4"><br>
Pitching to today’s Power Women<br>
</font></b><font face="Verdana" size="1"><br>
Outgoing, daring, free-spirited and ambitious, the modern day woman is a force 
to reckon with, both in the boardroom and outside. She is a rapidly emerging 
economic powerhouse and an avid consumer. How are marketers leveraging this 
growing ‘power of the purse’ and capitalising on women’s wealth? Nathalia Jones 
reports<br>
<i>
<br>
<img border="0" src="cover.jpg" width="200" height="492" align="left" hspace="0" vspace="3">“She’s – not – just a pretty face<br>
She’s – got – everything it takes<br>
She has a fashion line –<br>
a journalist for “Time”<br>
Coaches a football team<br>
She’s a geologist – a romance novelist<br>
She is a mother of three<br>
She is a soldier – she is a wife<br>
She is a surgeon – she’ll save your life<br>
She is your waitress – she is your judge –<br>
She is your teacher<br>
She is every woman in the world”</i><br>
<br>
When pop-star Shania Twain wrote this song for her album Up, she was not only 
creating a chart-topper that celebrated womanhood, she was also profiling the 
world’s fastest-growing economic powerhouse. With more women coming into the 
workforce and with increasing disposable income as careers take precedence over 
more conservative obligations of home and family, women have become key players 
in every country’s economy, a force to reckon with. Take a look at some of these 
interesting facts. In America alone, women make 80 per cent of all buying 
decisions. They wield control over US$7 trillion worth of purchasing power and 
surveys have forecast that by 2010, they’ll control more than US$13 trillion in 
private wealth.<br>
<br>
Globally too, the scenario is fast changing, as companies the world over are 
sitting up and paying attention to this rising seat of power. Brands such as 
McDonalds, Kodak, Fair and Lovely, Pepsi have had to do a complete re-think on 
strategy, for fear that the ladies would otherwise not loosen their purse 
strings. McDonalds, for instance, suffered a drastic sales slump in 2002 when 
its advertorials just used women as a vehicle to reach children. But since their 
recent revision in strategy – their new campaigns now target the ‘woman inside 
the mom’ – their efforts have resulted in the creation of a premium brand of 
salads and healthier, more figure-friendly happy meals. What’s more, even their 
play areas have been upgraded to include wireless workstations in recognition of 
the needs of moms on the go. This willingness to listen and understand that 
women don’t just perceive themselves as mothers helped the fast food brand build 
a better relationship with its female clientele, while winning over more 
customer patronage. You’ll find many such examples extending over a wide variety 
of products, goods and services across various segments from shampoos to fast 
cars that seek to tap the growing consumer power of women.<br>
<br>
Says Diane Girard, Founder and Managing Director of Canadian-based Global Links 
Network, “I find there’s been a tremendous shift in the past 10 years; I’ve 
travelled to over 40 countries and have had the benefit of seeing that there is 
an increased awareness of women’s economic power and it shows in the 
advertisements. Women are increasingly being considered; the colours are more 
appealing to a female audience. It’s very noticeable now that there is a gender 
balance in advertisements. Women not only have that participating power, they 
influence very much the decisions marketers make. In home décor, for instance, 
it is the women who influence the purchases that are going to be made. Women are 
buying more automobiles than men, so it’s something that manufacturers are 
increasingly aware of. It’s the same with mobile phones.” Diane was among the 
motivational speakers at the second annual Women in Business conference held 
last month in Muscat.<i><img border="0" src="cover1.jpg" align="right" hspace="6" vspace="3" width="241" height="300"></i><br>
<br>
Even when a brand is not exclusively meant for a woman, it certainly is targeted 
at getting her attention. Advertisers know that women today wield considerable 
clout and influence over household purchases. For instance, 80 to 90 per cent of 
campaigns in the white goods segment are targeted at catching the female eye and 
over 50 per cent of all marketing campaigns are directed at women. “Women are 
today being projected as opinion makers, when companies strive to market 
products bought by men. For example, cars, men’s perfume, men’s clothing, etc., 
most of the ads project that women approve of a particular brand in either or 
all of these categories,” says Haider Jawad Sultan, Managing Director, Capital 
Stores.<br>
<br>
Ajay Ganti, General Manager of SARCO, calls the modern-day woman an ‘important 
catalyst’ in any buying decision where she is valued as an advisor and 
consultant, who can enable informed and sensible decisions. Women, he says, 
“represent 100 per cent of the white goods markets, as they are the actual 
end-users in most categories; white goods are like a must-have accessory in a 
woman’s life, irrespective of her lifestyle.”<br>
<br>
In her corporate bestseller, The Power of the Purse, Fara Warner identifies 
three reasons why women are a key driving force in the world of advertising and 
marketing. An economic powerhouse, CPO of the household and thirdly, women’s 
entry into the workforce has had more impact on the global GDP than China, India 
or the internet.<br>
<br>
In the Arab world too, women are stepping up and taking charge, with the result 
that corporates and FMCG distributors are now positioning power women as a 
conduit to their brands. “Woman in the GCC are, therefore, a key focus for many 
marketers as they are the key decision-makers when it comes to choosing brands 
for the family and then, of course, for themselves. Although some countries like 
Yemen and Oman are seen to be slightly under-developed, the case remains the 
same. It is estimated that an Arab female spends on an average $345 per month on 
beauty care products and is fast forming a solid bonds with our goods,” says 
Iain Potter, VP HPC for Unilever, North Africa and Middle East.<br>
<br>
However, the region is still making progress in recognising the increasing 
buying power of women, as its female citizens are still breaking out of the 
mould and often find themselves torn between domestic and social obligations, 
and their own personal <br>
aspirations.<br>
<br>
At the recently held Women in Business conference in Muscat, one of questions 
discussed was ‘What drives the Arab woman?’ The ensuing debate described her as 
‘being an integral part of a very long tradition and having a clearly prescribed 
role in society’. While that is a very basic account of who and what an Arab 
woman is, it is a perception that is undergoing a sea change as more women in 
the region are empowered to enter the workplace.<br>
<br>
As a result, they now have greater spending power and with it comes the freedom 
to spend and splurge. They are doing this and how! In Saudi Arabia alone, around 
30 per cent of all new car purchases were made by women according to researched 
reports. “Today’s Arab woman is aware of international trends and new launches. 
She cannot be misled or misinformed. She is constantly in touch with the media 
of all kinds. Most Arab women travel overseas and visit international retail 
chains such as Harrods, Saks, 5th Avenue, etc. They read international 
magazines. Therefore, it is essential to update product collection in stores. It 
is also imperative to offer adequate ambiance and services,” says Jawad Sultan 
of Capital Stores.<br>
<br>
<i>
<img border="0" src="cover3.jpg" align="left" hspace="6" vspace="3" width="262" height="286"></i><b>What women want</b><br>
When it comes to a particular brand or product, what women really want is 
‘reliability,’ according to sources from the advertising and marketing world. 
“Peer group, word of mouth and constant advertising are what drives their 
choices. As does the fact that they can now stand up to the rigours of the 
economic world and make a name for themselves. Then there’s also the reduced 
dependency on their male counterparts that make women a driving force across the 
globe spanning industries, politics and so on,” say company sources. Women 
increasingly want to be trendsetters as far as consumption habits are concerned; 
they want to eat well, dress well and travel. This is a reflection of their bold 
and outgoing spirit; a sign that they are ready to take risks in terms of 
product usage; they are ready to explore and experiment with different brands 
and products all the time. “This, in turn, drives marketers to showcase their 
products to the female audience.”<br>
<br>
