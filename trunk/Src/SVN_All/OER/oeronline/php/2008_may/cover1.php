		<td rowspan="2"><font face="Verdana" size="1">Another major determining buying criterion when it comes to women is the luxury 
quotient that goes beyond just functionality and durability. According to Dr Concetta Lanciaux, Strategy Luxury Advisor, Advisor Groupe Arnault, and one of 
the speakers at last month’s Women In Business Conference, “while commodities 
are function specific, luxury goods create dreams; these refer to products that 
respond to people’s dreams as opposed to their needs.”<br>
The availability of luxury products is being underpinned by larger disposable 
incomes of women, according to Potter of Unilever. An example of this is the 
fast pace of growth of the face care market in Arabia. It is valued at US$156 
million and is growing at a whopping 12 per cent (2006 vs. 2007; AC Nielsen). 
More women are investing a more in skin and beauty products, specially as 
external appearance is directly linked to personal image and confidence. 
“Unilever is currently the market leader in the GCC, with its face care brand 
Fair and Lovely,” says Potter. “We find marketing to women is essential in 
driving category growth and consumption of our brands. Take ‘Fair and Lovely’, 
for example, a personal choice brand that we can safely say has captured the 
local female population through a very vigorous research process that 
understands what female needs are,” he adds.<br>
