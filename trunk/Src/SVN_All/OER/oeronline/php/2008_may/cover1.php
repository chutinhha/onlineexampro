<p class="MsoBodyText"><font face="Verdana" size="1"><b>Opting for luxury brands</b><br>
In the Arab world, the luxury ratings of any product are given serious 
consideration and are a crucial factor in buying. “The Arab woman’s need for 
luxury branding is higher,” said Sangeetha Pendurkar, Chief Marketing Officer – 
Middle East, HSBC Bank, and also one of the participants at the Women in 
Business Conference.<br>
<br>
That statement just begs the question why are luxury brands accorded such a high 
degree of preference and priority among Arab women? Well according to Adelina 
Mustata, Client Service Director, Synovate, premium brands and luxury “are used 
as a means to overcome the feeling of inferiority and demonstrate self-worth”. 
Adelina who discussed ‘Arab women and luxury’ at the Marketing to Women 
conference, explained this gravitation towards premium products as the Arab 
woman’s portrayal of herself as one who is ‘smart, up-to-date, knowledgeable, 
distinguished and original’. Above all, it is a sign of success. The four key 
emotional pillars for what she calls ‘premiumness’ are strength/power, beauty, 
prestige and comfort.<br>
<br>
A comparative study between two major GCC markets – the UAE and Saudi Arabia – 
on what drives women’s choices in luxury brands threw up some interesting and 
insightful revelations at the conference. While in the UAE, it was found choices 
were more personality driven – that is how much of a reflection of one’s 
personality the product was – in Saudi Arabia comfort and high standards that 
were still nonetheless strongly grounded in social values were the drivers. The 
choices here leaned more towards femininity and beauty as opposed to the UAE 
where ambition and career aspirations ruled many of the luxury purchases by 
women.<br>
<br>
“From the point of view of the marketer, how does your product/brand engage with 
these women?” was one of the questions put forward by Sangeetha of HSBC. The 
answer lay in designing brands that delivered on comfort, confidence and 
control; ‘brands that drove confidence and those which helped women to meet 
challenges in the new world’.<br>
<br>
No discussion on luxury and the Arab woman can be complete without a reference 
to cars. Almost everywhere in the GCC, and certainly in Oman, you’ll find it’s 
often a woman behind the wheel of all top-end models. Top-of-the-range 
automobiles spell freedom, independence, good taste, verve and vitality. And 
it’s this combination of speed-propelled power and stylish elegance that seems 
to drive choice. According to Adelina, the fundamental meaning of cars to Arab 
women centres on five core principles – individuality, social identity, 
mobility, a source of power and social positioning, its acquisition gives her a 
feeling of strength and freedom. Again a study between the UAE and Saudi Arabia 
on the subject found that while in the UAE owning a vehicle meant being 
independent for transport, in Saudi Arabia the same car gave the woman some 
authority and say in actually buying the vehicle, even though she was not going 
to be in the driver’s seat. Either way, it allowed the Arab woman to stay on top 
of trends, which was her way of compensating for a lack of self-confidence and 
often low self-esteem. Not surprisingly, owning an automobile gave her a sense 
of control in her life and in many ways it brought about gender parity.<br>
<br>
So what marketers need to look at, according to Adelina, are the different 
layers, which are first and foremost the meaning and the influence of the 
cultural context, the fundamental meaning of the category, their specific and 
individual needs and motivations regarding specific products.<br>
<br>
<b>For the best in beauty</b><br>
The beauty and cosmetics segment is perhaps <br>
the largest selling segment when it comes to women, specially in the Arab world, 
where a lot of emphasis is placed on external appearances and personal image.<br>
<br>
Says Unilever’s Potter, “With the influx of more women in the business arena, 
the power of spending on female based products is a lot more substantial in 
growing markets around the world. For instance, the beauty care market is seeing 
a global growth of around 17 per cent and is estimated at US$2.5 billion per 
annum.”<br>
<br>
Apart from cosmetic products, long-term beauty procedures such as plastic 
surgery are fast gaining acceptance. “Oman’s booming economy has certainly 
contributed to the desire by creating a platform for wealth and exposure to the 
means of medical cosmetic services that women of all ages deserve,” says Dr 
Fatma Obaid Ali, CEO and <br>
Managing Director of Emirates Medical Centre in Muscat. On what is fuelling the 
growing preference for medical cosmetic procedures, she says, “Even considering 
that Oman is a fairly young developing country, EMC’s growth has been nothing 
less than overwhelming. I would say that specific demand for the more serious 
medical cosmetic procedures started to boom in Oman around three years ago, with 
the most significant growth in the past 18 months or so. The medical approach to 
beauty today is fast moving towards a much more holistic approach, rather than 
just the cure. Anti-aging now includes advice on your mental health, your diet, 
exercise and a number of other influencing factors.” Women at the EMC, she adds, 
make up 80 per cent of the centre’s client base, while it serves approximately 
4,500 clients very month.<br>
<br>
But are marketers and distributors leveraging the potential of this market to 
get maximum mileage for their brands, or are women in this segment 
under-represented and under-marketed to? Says Dr Ali, “Even with very visible 
wealth creation due to the dynamic development of our country, in the past 
couple of years, women and their ever-increasing needs are not taken seriously 
enough.”<br>
<br>
As mothers and career women nowadays, women have a lot move spending power than 
ever before and various stakeholders, like marketers have yet to become serious 
enough about the needs of the women in Oman regarding medical cosmetic services. 
One of the major constraints is that very little or no market research has been 
done in order to generate statistics on which small and medium enterprises can 
base their decisions. You either need to initiate your own rather costly 
investigations or surveys, or settle to base critical business decisions, either 
blindly or on perceived influences in the market. It has also been quite a 
challenge to acquire a suitable level of competence from marketing consulting 
firms in Oman,” Dr Ali concludes<br>
This scenario, she adds, contrasts with other countries in the GCC, such as the 
UAE, where women have the advantage of operating well within developed 
environments where statistics are freely available. “Their environments are also 
a lot more exposed to globalisation and media infrastructures there are a lot 
more active, with a variety of high class women magazines and other media 
platforms like TV, which provide a much greater exposure than what we still 
experience in Oman,” she adds.<br>
<br>
<b>Staying in fashion </b><br>
While the cosmetics and beauty segment in Oman may need to do a rethink on 
strategy for its female customers, the country’s fashion industry needs even 
more thought and strategy re-visioning to tap into women’s wealth. Considering 
that women in the region and certainly in Oman are gravitating towards hi-style 
and haute couture, designers feel that not enough is being done to capitalise on 
the interest. Says Samah Al Wahaibi, owner of Layali Al Asalah event management 
company as well as an employee at Oman Oil Company SAOC. “Marketing fashion to 
the Omani woman is still a challenge, specially with the case of designer wear 
because the Omani woman is still prioritising. Oman itself is new to the fashion 
world and so the industry cannot really compete on a regional or global scale. 
If you take the whole country itself, there is only Muscat where there is some 
sense of fashion or designer fashion, and one can hardly judge entire Oman by 
Muscat alone.”<br>
<br>
Adds Diana Al Zadjali, another fashion designer, “I do recognise that Oman has a 
lot of business to offer because of this rising fashion consciousness among 
women, but we as designers are hampered by the lack of technology. Fashion, as 
you know, is heavily dependant on technology because it’s a global industry and 
Oman needs that kind of cutting edge technology for it to come to par with the 
industry on a global scale. What we also need is international expertise and 
skill for design development. This is what will give us better reach and 
understanding with our customers. Another important thing we need to understand 
is that because times and trends are changing in this industry, we need to adapt 
and adopt – adapt to the changing times in terms of technology and support 
infrastructure and adopt competitive skills to develop design competence. Yes, I 
do feel that the female market group in this sector is grossly under-tapped and 
we need more fashion shows and the like to showcase designer wear to the modern 
Arab woman.”<br>
<br>
Samah feels that there has been a change in the mindset with globalisation and 
liberalisation, and yes, women out here are keeping abreast of the latest 
trends. But to say that they would invest and splurge large sums of money on 
designer wear is not entirely correct, because if they have to choose between 
that and say a car, they would choose the car over designer clothes. However, 
she does recognise that there is huge untapped potential for the fashion 
industry among women in Oman. Two major ways, according to her, to make good on 
the opportunities are setting up of fashion training institutes and removing any 
social stigma associated with fashion in the Arab world. “I think Oman needs to 
have training centres to train young, talented designers. Often, I find a lot of 
young people with the passion for design and fashion but one must recognise that 
fashion after all is a profession and a business, and like any other business, 
it needs skilled expertise to grow and succeed. Another issue is the cultural 
hurdle; our society does not accept women in the fashion business. Even in 
Dubai, which is often considered the fashion capital of the Arab world, women in 
the business are up against many social barriers. So, I think for the industry 
to fully exploit and capitalise on the potential of women’s growing consumer 
power, there has to be a change in the way we think.”<br>
<br>
Eve-olutionising strategy – ‘Let’s give ‘em something to talk about’<br>
“Knowing and understanding what drives women consumers has profound implications 
on marketing strategies,” said Adelina during her presentation at the recently 
concluded Women in Business conference. The conference demonstrated how when it 
comes to women, ‘it’s all about how products ‘feel’ in social settings’ rather 
than grand promises made by million dollar ad campaigns. “Today’s consumer is 
much more inclined to seek out information from sources they trust,” pointed out 
Dr Traek Mady, Chair, Department of Marketing and Marketing Communications, The 
American University in Dubai, at the conference. Traditional marketing then is 
just not cutting it any more as non-marketer dominated sources such news, 
critiques, club/organisations, family, friends, neighbours and casual 
acquaintances are increasingly preferred as product/brand reference points. This 
makes word of mouth the number 1 source influencing food and household product 
purchases. <br>
<br>
What then are the implications for marketing to women? According to Dr Mady, 
“Women make decisions by consensus not because they are wishy-washy or affective 
by nature, but because they are critical thinkers who want to consider a range 
of options.<br>
&nbsp;Understanding the nuances of word of mouth among women and embracing the 
unique way women share information is an extremely effective strategy for 
marketing to women.”<br>
According to Ganti of Samsung, marketers need to revisit strategy to ensure it 
embodies a more ‘holistic approach’ that connects with the EQ or emotional 
quotient of the fairer sex. “Any strategy which is set against the backdrop of a 
relationship with the product’s or brand’s significance in the foreground will 
have a more far reaching impact. Women would then be able to relate to the 
product/brand and understand its advantages at the same time.”<br>
<br>
Adds Diane, “Is there enough marketing done to business women? I would say in a 
lot of places yes, but there’s still some shifting that needs to take place, 
change in mindset and I think women need to be consulted a lot more in their 
environment. They can’t readily adapt strategies that have been used for men in 
the past, it’s just a totally different way that women think and network. Women 
are more word of mouth and referrals.”<br>
<br>
Good word of mouth strategies, says Dr Mady, must encourage communication, one 
should invest in listening, give women incentives to be the product’s/brand’s 
marketer, and should entail strategies that engage in transparent conversations 
and information sharing. In short, these strategies should give women something 
to talk about.<br>
<br>
And companies everywhere are certainly giving the ladies a lot to talk about. 
The world over, banks, financial institutions, FMCG distributors and 
manufacturers are waking up to the women’s wealth. In the Gulf too, the 
phenomenon is catching on. With women accounting for a significant and growing 
proportion of the Gulf’s capital, both financial and human resource, banks and 
investment funds are alerting themselves to the opportunities for providing 
services and products to women and women-run companies. According to Bramdean, a 
London-based fund management firm, women account for some $240 billion of 
personal wealth and 60 per cent of that is held in cash. In Saudi Arabia, women 
hold roughly one-third of the bank accounts, with deposits of over $7 billion.<br>
<br>
In her presentation at the Women in Business conference, Jane Kinninmont, 
Editor, The Economist, Economist Intelligence Unit, UK, pointed out that some 
estimates show 40 per cent of Saudi real estate is held by women, though they 
own only 20 per cent of investments in mutual funds.<br>
<br>
But there are challenges as marketers endeavour to capitalise on women’s wealth. 
Says Ganti, “The biggest challenge is to build up trust with a brand or product. 
Women don’t trust that easily and once they do, they expect a lot in return. 
Also, marketers have to be quite vigilant, innovative and alert with new ideas 
and strive for constant change in their presentation, with new additions to 
products/services, which are all imperative to keep the freshness of the idea 
intact.” </font></p>
