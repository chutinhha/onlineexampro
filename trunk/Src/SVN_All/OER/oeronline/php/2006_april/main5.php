</font></b><font face="Verdana" style="font-size: 14pt"><b>A Touch of Class</b></font><b><font face="Verdana" size="1"><br>
Besides uncovering 
the dramatic success of Al Qandeel Real Estate Services, OER looks at the issues 
which concern the real estate sector in the Sultanate<br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<img border="0" src="cor_pro.jpg" width="137" height="222" align="left" hspace="5" vspace="2">M</font></b><font face="Verdana" size="1">ustafa 
Ali Mohammed, the founder and owner of Al Qandeel Real Estate Services, is the 
man behind the concept of “installment” in real estate in Muscat. The concept of 
buying and selling property on installments in Oman was made popularby him. <br>
<br>
Mustafa recalls: “It took me a year to convince people that this concept is 
feasible, and that it is an acceptable practice the world over.” Established in 
1984, Al Qandeel was initially into the sale and purchase of properties. But 
today, the company is engaged in other real estate activities too, including 
construction, valuation, property development, rening in and renting out, and 
selective engagement in contractor’s assignment based on client profile. “Today, 
we are known for our competence, quality, trustworthiness and a great customer 
service,” <br>
says Mustafa.<br>
<b><br>
Liquidity Issue</b><br>
The company is now spreading its wings beyond Oman also. It’s coming up with the 
52-storey Malak Towers at Sharjah, UAE, with 139 apartments. Real estate is 
booming in the Middle East. And Muscat is no exception. In fact, Muscat provides 
an equilibrium between open space and construction activity, which is a key 
element in any classy construction endeavour. The bottleneck, if any, is in the 
form of tight liquidity in the market, with banks providing just 50 per cent of 
the estimated cost of any project. “Being dependent on bank loans, our 
fraternity would be greatly aided if this 50 per cent could be increased to, 
say, 75 per cent,” Mustafa points out. <br>
<br>
According to him, the spate of construction activity that one witnesses in the 
UAE is largely a fall-out of easy availability of bank loans and ownership laws 
that allow property ownership to foreigners. <br>
<br>
Mustafa adds: “Construction business is set for a quantum leap in the near 
future. Industries are coming up in a big way. The travel and tourism sector is 
growing rapidly. Various infrastructure and other developmental projects are in 
full swing. Add to this, local employment is also going up. All these pointers 
get reflected in the growth of real estate sector. But the single most important 
boost is the government’s decision to allow foreigners to own athe market-size 
gets expanded many fold. In the next five years, this sector will show 
significant growth. We will also see companies coming up with more housing 
projects and related activities.”<br>
<b><br>
Current Projects</b><br>
In Oman, the current projects of the company include Shining Shatti at Shatti Al 
Qurum, Candles at Ghubrah and Al Salaam at Qurum Plaza. Shining Shatti, 
comprises 204 fully furnished flats with a basement parking with round-the-clock 
security and a business floor consisting of shops, a restaurant, supermarket and 
other showrooms. The company has also conceived Candles, a RO35 million-project, 
in the idyllic setting of Ghubrah Beach, with 409 apartments. <br>
<b><br>
Trendsetter</b><br>
The single achievement that gives the Al Qanteel chief a lot of satisfaction is 
that his company has been able to set the trend of property development in Oman. 
While most of his competitors develop a property and rent it out, Al Qanteel 
goes a step further and sells the properties as residential units. <br>
<br>
The prospects of the allied businesses of Al Qandeel, like renting in and 
renting out, have also improved with the boom in the construction sector in the 
Sultanate. The rental business has seen a quantum jump in the wake of the 
construction activity that one currently sees happening in Muscat. The general 
scenario of the construction sector has been looking up, with both the demand 
and supply moving up. One big difference which is changing the real estate 
sector in Oman, according to Mustafa is the concept of building and selling new 
apartments and villas as a project. Now, companies are coming up with projects 
in Oman offering apartments/villas for sale.<br>
<br>
Al Qanteel encourages Omanisation wholeheartedly. In the important function of 
project sales, where it has to interact with high networth individuals as each 
of the residential units is of a very high value, the entire team consists of 
Omanis only.<br>
