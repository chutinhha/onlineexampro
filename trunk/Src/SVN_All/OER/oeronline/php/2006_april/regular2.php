</font><b><font face="Verdana" size="5">Ahmed says ‘Get Out More Often’</font></b><font face="Verdana" size="1"><b><br>
Matt Harrison explores the 
murky world of conferencing and suggests it might be a good idea to get out a 
little more often<br>
</b><br>
<b>Ahmed’s Tale</b><br>
Let’s fast forward to 31 December 2006, you’re at a friend’s place welcoming in 
the New Year and you meet Ahmed — he’s in his late 30s, an engineering graduate 
from SQU, has an MBA from London Business School, he’s a member of the Chartered 
Institute of Marketing and the MD of SALT, a software company based at Knowledge 
Oasis Muscat. Academically, he’s covered all the bases; professionally, he’s got 
just shy of 15 years experience&nbsp; under his belt. You might well think Ahmed 
wouldn’t need any further training to help him or his business grow. But he 
does. 
Dinner’s finished and your guest’s serving coffee and Ahmed begins to tell you a 
story. <br>
<b><br>
Bin It </b><br>
“Muscat-based business conferences and seminars, now, how many of 
these brochures come across your desk each week?” “Lots” I say. “Right,” says 
Ahmed. “Most of the time I just bin them. But there was one that really caught 
my eye. It was right up our street. SALT had been growing, particularly over the 
past two years, we’d had a couple of lucky breaks and things were beginning to 
drop into place. But the thing about this particular conference — and this is 
important — it was as an interesting mix of presenters from various backgrounds 
but they were all grappling with the same set of business problems. Lots of case 
studies, warts and all. I’ve been to the academic-type conference, you know, the 
ones where you begin to nod off half-way through the keynote session. What I was 
looking for were ideas. I’m starved of them, aren’t you? I wanted to listen to 
how others were fairing and solving problems. I wanted a more hands-on, 
practical experience and an opportunity to meet and talk with peers that were 
facing similar business problems. <br>
<br>
The conference ran for two days and had over 20 presenters drawn from IT, 
finance, marketing, IT, HRD, government and start-up backgrounds, it was a real 
cocktail of players. All in Muscat and at a very affordable price, that pleased 
our CFO! <br>
<br>
It was a struggle making sure I had the time to go, re-arranging my diary, and I 
had all my calls re-directed to a colleague in the office. I’d spent hard-earned 
cash for the two days, so I wanted to focus on what I’d paid for. Believe it or 
not, I didn’t miss one session. But it wasn’t just about me and my personal 
development. The best thing about attending the conference was what came after — 
passing on what I had learnt to colleagues in the firm. In fact, I set aside two 
post-conference meetings just to go over what I’d picked up at the two-day event 
and brief the team on who I had met. It wasn’t just the sessions, it was the 
coffee breaks, the lunches and the gala dinner, swapping cards and ideas and 
meeting folk in a relaxed atmosphere. It was a really profitable experience.<br>
<b><br>
Training &amp; Development</b><br>
One of the things we really value at SALT is not just training but development. 
Equipping our staff with the right skills they need to do their jobs, ranks as a 
top priority. But the more I learnt at the conference, the more I realised we 
weren’t doing enough. Since October, training and development, whether that’s 
in-house or external, has become an even more important part of the firm’s 
day-to-day activities. <br>
<br>
Another thing that had a profound impact on me, was the opportunity to sit back 
from the mania of day-to-day business, get away from office, take some time out 
and reflect on what we do and how we do it. In fact, the conference gave me the 
time to evaluate SALT’s business thoroughly. We were already doing some of the 
right things by default. For example, I’d chosen from the very beginning to 
produce a premium product rather than compete on cost, buy cheap, buy twice, 
right? But the conference forced me to really think about how we operated. For 
example, our relationships with customers, how we dealt and communicated with 
them. I realised that, like many companies, we weren’t concentrating on the 
customers who gave us the most business. Once you realise something like that, 
you start acting on it. <br>
<br>
The conference was also great for meeting peers and exchanging ideas. Delegates 
were mainly owners, CEOs and GMs from a wide range of businesses — energy, 
telecom, IT, investment, publishing, manufacturing, insurance and banking, an 
eclectic mix of players. Speakers were from start-ups, scale-ups through to 
multinationals, talking on finance, management, human resource development, 
e-commerce, law and technology. It was all so relevant, looking at case studies, 
strategies and techniques, all of which could be applied directly to SALT. I 
could pick-up something in a morning session, make a call to a colleague in the 
office during the lunch break and have a feedback memo on my desk within 48 
hours. That immediate.<br>
<br>
<b>
<img border="1" src="marketing.jpg" width="200" height="166" align="right" hspace="5" vspace="2">Piracy</b><br>
Sure, there were parts of the conference that didn’t immediately appeal — legal 
issues, for example. But even there, I picked up a number of pointers. Copyright 
and IPR issues, now these are critical to my business, issues that can cut deep 
into the profitability of a software company. It was interesting to hear how 
regional and international agencies are partnering with local businesses in an 
effort to eradicate pirate software and push the IPR debate. <br>
<br>
<b>Change</b> <br>
Sure, if I hadn’t attended the conference, SALT would have continued to grow. 
But having been, I feel we’re better equipped for the growth that’s coming our 
way. We’re more knowledgeable, more professional and I made some excellent 
contacts that we’re capitalizing on. Without doubt, taking two days out has 
altered how we operate.” Will you be telling a similar tale this New Year’s Eve?<br>
<i><br>
Matt Harrison is a Muscat based marketing consultant.</i></font></p>

