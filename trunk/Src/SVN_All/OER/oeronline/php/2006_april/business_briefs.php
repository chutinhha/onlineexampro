                        government’s commitment to promote Omani products. 
                        Tawfeeq A. Al Lawati, Chairman ADC, said that the export 
                        trade between ADC and the Iraqi government was 
                        facilitated by OCIPED which has helped ADC to bag the 
                        US$10 million Iraqi contract, a one-time deal. Lawati 
                        further commented that OCIPED has been making serious 
                        efforts in finding new export markets for Omani 
                        products. OCIPED has identified Syrian and Iraqi markets 
                        where Omani products have found good response. <br>
                        <br>
                        There are 
                        several Omani products which can compete well in the 
                        international market and OCIPED works hand in hand with 
                        the local businessmen to tap new export markets, 
                        commented Faris Nasser Mohammed al Farsi, Director of 
                        Facilitation in Export Marketing, OCIPED. </font></p>
