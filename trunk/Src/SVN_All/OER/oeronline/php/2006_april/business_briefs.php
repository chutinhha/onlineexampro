                        <p align="justify"><font face="Verdana" size="1"><b>
                        <img border="1" src="bsbr4.jpg" width="120" height="78" align="left" hspace="5" vspace="2">Gulf 
                        Air’s new maintenance deal</b><br>
                        Gulf Air has signed a memorandum of understanding (MoU) 
                        with Swiss-based S R Technics Holding for fleet 
                        maintenance. As part of this MoU, S R Technics will 
                        assume complete responsibility for repair, operation and 
                        maintenance of the airline’s fleet. <br>
                        <br>
                        The five-year 
                        contract, valued at $750 million, also provides for the 
                        establishment of an aircraft maintenance facility and 
                        training center to be based in Oman. The Oman-based $50 
                        million three-day hangar and maintenance facility in 
                        which Gulf Air will have 10 per cent shareholding, will 
                        provide maintenance solutions, including aircraft and 
                        component services, technical training, logistics, 
                        on-wing and light engine services and engineering. Using 
                        its integrated airline solutions product, S R Technics 
                        will provide full fleet technical management for the 
                        airline’s fleet which comprises 10 A-320, six A-330, 
                        nine A-340 and nine B767-300 aircrafts. While fleet 
                        management will be undertaken from Switzerland, 
                        maintenance and repair services will be provided through 
                        the joint venture, local partnerships and S R Technics’ 
                        global component logistics network.</font></p>
