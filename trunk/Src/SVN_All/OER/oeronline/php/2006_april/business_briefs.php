                        OCIPED role in export promotion appreciated</b><br>
                        <b>
                        <img border="1" src="bsbr3.jpg" width="120" height="131" align="right" hspace="5" vspace="2"></b>The role of the Omani Centre for Investment Promotion 
                        and Export Development (OCIPED) in promoting Omani 
