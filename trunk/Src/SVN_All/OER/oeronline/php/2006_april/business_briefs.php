                        <p align="left"><font face="Verdana" size="1"><b>
                        Renaissance Services approves 25 per cent dividend</b></font></p>
                        <p align="center">
                        <img border="1" src="bsbr1.jpg" width="300" height="188"></p>
                        <p align="justify"><font face="Verdana" size="1">The 
                        shareholders of Renaissance Services SAOG held an Annual 
                        General Meeting (AGM) at the Muscat Securities Market 
                        (MSM) Meeting Hall, where they approved the company’s 
                        Annual Report for 2005. The company has reported record 
                        results for the year, achieving revenue of RO 106.4 
                        million, EBITDA of RO 28.4 million and net profits of RO 
                        13.9 million.<br>
                        <br>
                        The company has also delivered considerable Shareholder 
                        Value during the year. Net worth is up from RO 24.9 
                        million to RO 68.6 million; and the company’s Market 
                        Capitalization increased from RO 39 million to RO 118.6 
                        million. The Shareholders also approved a cash dividend 
                        of 25 per cent at the meeting.<br>
                        <br>
                        An Extraordinary General Meeting (EGM) was held 
                        immediately after the AGM, at which shareholders 
                        approved a stock split of 1:10, whereby the nominal 
                        value of the share has changed from RO 1 to 100 baisas. 
                        This move is expected to improve the liquidity of the 
                        share in making the stock more accessible to retail 
                        investors.<br>
                        <br>
                        Renaissance Services SAOG is one of the largest public 
                        listed services companies in the region. It has 
                        businesses engaged in IT, media and training; it is a 
                        leading turnkey contract services provider in the Middle 
                        East; and has one of the top 10 marine support fleets in 
                        the world with a significant presence in the Gulf, the 
                        Caspian region and S.E. Asia. Renaissance is recognized 
                        as an Omani company with significant international 
                        credentials; successfully exporting its services abroad 
                        as well as expanding through merger and acquisition. The 
                        company’s Annual Report states that 75 per cent of the 
                        annual revenue of RO 106.4 million was generated outside 
                        Oman and 60 per cent of revenue is generated in services 
                        to the oil &amp; gas industry.</font></p>
                        <hr width="90%" size="1">
                        <p align="justify"><font face="Verdana" size="1"><b>The 
                        Banker award for Omantel</b><br>
                        Oman Telecommunications Company (Omantel) has won the ‘Deals of the Year’ Award 
                        from The Banker at a ceremony held in Dubai. Omantel was 
                        selected among other Omani companies on the basis of the 
                        largest IPO in Oman. The Director, Corporate Affairs 
                        Unit of Omantel, Eng Abdulhaleem bin Ali Al Sabbagh, 
                        received the award on behalf of Omantel. Apart from 
                        Oman, the other countries that were represented in the 
                        competition included companies and banks from the UAE, 
                        Qatar, Bahrain, Kuwait, Saudi Arabia and Egypt.<br>
                        <br>
                        Omantel has also won the 2006 Gulf Economic Award 
                        organised by the Dubai-based Datamax Company for 
                        organising exhibitions and con-ferences. The award is 
                        given annually to organizations that play a positive 
                        role in fostering growth in the economy of the Gulf 
                        region, attracting foreign investment and realizing 
                        practical results through sound management and 
                        leadership. </font></p><hr width="90%" size="1">
                        <p align="justify"><font face="Verdana" size="1"><b>
                        Sohar Aluminium suppliers’ meet</b><br>
                        Sohar Aluminium held a suppliers’ meet, targeted at the 
                        local business community with the objective of informing 
                        them about the progress of the project and business 
                        opportunities during the construction phase. The meet 
                        was attended by representatives of the construction 
                        industry and material suppliers and service providers to 
                        the industry.<br>
                        <br>
                        Tony Kinsman, CEO, Sohar Aluminium, said, “One of the 
                        key objectives of this project has been, and will always 
                        be, to provide opportunities for local business where 
                        possible and forums like this allow us to share 
                        information on such opportunities with the business 
                        community.” Representative from Bechtel, the EPCM 
                        contractor, made an extensive presentation outlining 
                        project expectations, opportunities for suppliers and 
                        the bidding process that will be followed by the 
                        contractor. The team listed more than RO 72 million 
                        worth of opportunities for local industries. Overall, 
                        between the smelter and power plant, more than RO 97 
                        million worth of opportunities for the local businesses 
                        were presented.</font></p><hr width="90%" size="1">
                        <p align="justify"><font face="Verdana" size="1"><b>
                        <img border="1" src="bsbr2.jpg" width="120" height="147" align="left" hspace="5" vspace="2">IIF’s 
                        9th MENA Bank Chief Executives’ Meet</b><br>
                        BankMuscat hosted the Institute of International 
                        Finance’s (IIF’s) 9th Annual Middle Eastern and North 
                        African Bank Chief Executives Meet in the last week of 
                        Fe-bruary. Senior bankers from 82 members of this global 
                        association met in Muscat to discuss the challenges and 
                        opportunities across the MENA economies, the outlook to 
                        global energy markets, fin-ancing regional projects, the 
                        growing role of private equity in global investment 
                        flows and the keys to success in consumer finance. 
                        Issues pertaining to corporate governance and the BASEL 
                        II accord were also part of the agenda. <br>
                        <br>
                        The proceedings were led by Charles Dallara, Managing 
                        Director of IIF. Speaking on the occasion of the meet, 
                        AbdulRazak Ali Issa, the BankMuscat Chief Executive, 
                        said: “It is indeed a great honour to be hosting a 
                        gathering such as this. We are extremely glad and 
                        grateful to have been given this opportunity. <br>
                        <br>
                        The IIF today plays a significant role in helping the 
                        financial community work closer together and better 
                        appreciate the risks and the challenges that lie ahead, 
                        develop industry best practices and influence matters of 
                        policy. We are glad to be a part of such an august 
                        institution.”</font></p><hr width="90%" size="1">
                        <p align="justify"><font face="Verdana" size="1"><b>2nd 
                        GCC-India Business Conference</b><br>
                        To strengthen, consolidate and promote the economic 
                        relations between the GCC states and India, the second 
                        GCC-India Business Conference was held in Muscat under 
                        the motto “GCC-India: Partnership and Cooperation”. 
                        Apart from the government dignitaries from both the 
                        sides, officials of the major delegations representing 
                        the private sector like the Gulf Organization of 
                        Industrial Consultant (GOIC), the Federation of the GCC 
                        Chambers and other business delegations took part in the 
                        conference. The conference has been a step forward in 
                        paving the way for promotion of trade, economic 
                        relations and investment between the GCC Sates and 
                        India. It has looked at opportunity for creation of 
                        joint investment projects and facilitation of bilateral 
                        trade between the two sides. </font></p>
                        <hr width="90%" size="1">
                        <p align="justify"><font face="Verdana" size="1"><b>OGWA 
                        exhibition and conference in April</b><br>
                        The Oil &amp; Gas West Asia (OGWA) 2006 exhibition and 
                        conference will be held in the first half of April at 
                        the Oman International Exhibition Centre. C.J. Paul, 
                        General Manager of Omanexpo, the company organising the 
                        show, said, “Space bookings for this year’s exhibition 
                        have increased by over 20 per cent compared with the 
                        2004 edition of the event. <br>
                        <br>
                        This clearly demonstrates the high regard that 
                        international contractors and suppliers give to Oman as 
                        a major market for oilfield services as well as products 
                        and technologies for the oil and gas industry.” Over 250 
                        companies will be represented at the biennial event. 
                        Alongside all major energy companies from Oman, OGWA 
                        2006 will also feature exhibitors from Bahrain, the UAE, 
                        China, Iran, India, Kuwait, Malaysia and Singapore. 
                        Among the major players that will participate are PDO, 
                        Oman LNG, Oman Refinery, Total, Occidental Oman, the Abu 
                        Dhabi National Oil Company, Dolphin Energy, Halliburton, 
                        Schlumberger, Reliance Industries, Gujarat State 
                        Petroleum Corporation and Petrofac.<br>
                        <br>
                        Pipeline magazine, in partnership with OGWA 2006, will 
                        hold the first-ever Innovation in Energy Awards-Oman on 
                        the first day of the conference and exhibition. The 
                        awards will recognize the achievements and sound 
                        business practices of oil and gas companies currently 
                        operating in the Sultanate. Outstanding players in the 
                        local oil and gas industry will be honoured in five 
                        distinct categories: innovation in oil, innovation in 
                        gas, innovation in petrochemicals, innovation in new 
                        technology and project of the year. “All industry 
                        professionals should take advantage of this opportunity 
                        to express their opinion on which companies in Oman 
                        truly deserve to win the awards,” said Eileen Michael, 
                        publisher of Pipeline Magazine. Winners are going to be 
                        chosen by the very people who work for this industry, 
                        through a unique reader’s choice scheme that requires 
                        participants to explain their vote.</font></p>
