            <p align="justify"><font face="Verdana" size="1"><b>2006: Watch Out For Brand Oman</b><br>
            Oman is, arguably, the most interesting hotel market to watch out 
            for in 2006. The arrival numbers are swelling, and there are all 
            indications that it is growing at a rate where it is bound to carve 
            out a niche for itself.<br>
            <br>
            What’ more, the potential future developments – most of them are 
            destination-focused resorts - here are spread across right across 
            the length and breadth of the country, rather than centred around 
            Muscat. The strong development regions are Sohar, Ras Al Hadd, 
            Musandam, and, of course, Muscat.<br>
            <br>
            A peep into the performance of the hospitality industry in the first 
            two months of 2006 clearly tells the tale. The Chedi has registered 
            occupancy of 95 per cent in this period.<br>
            <br>
            Al Bustan’s Zamora says judging 2006 by its first quarter, it’s 
            going to be an excellent year. “We are ahead of last year in budget 
            and average rate. I look at 2006 and 2007 as offering excellent 
            prospects for the tourism industry.” 2005, he says, was better than 
            2004 and 2006 will be better than 2005. “However, you must be 
            practical and realistic. Dubai is doing very well. Dubai occupancies 
            are higher than ours in the summer months. I think our government is 
            taking the steps so that we have greater occupancies in the leaner 
            months. But the industry as a whole in the ME is booming. We are 
            emerging as an equal player with the best.” <br>
            <br>
            Brandes guesses the industry is going to continue like it did last 
            year. He even hints at the problem of plenty, saying his hotel is 
            already, almost always full. “We actually cannot have more 
            occupancy,” he points out.<br>
            <br>
            Oman boasts of some of the most majestic hotels in the region. Now, 
            with Shangri-La opening, the marketing reach is getting stronger. 
            There are more operators in the market. And the more the operators 
            in the market, the wider the reach of the marketing! <br>
            So, the gist is, watch out for the Omani hospitality industry in 
            2006! </font></p>
