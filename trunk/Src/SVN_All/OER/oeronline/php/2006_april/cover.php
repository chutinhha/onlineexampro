            <p align="justify"><font face="Verdana" size="1"><b>The Challenges</b><br>
            What about the challenges facing the hotel industry? Are they being 
            brushed aside in the din of soaring occupancy and room rates? Pike 
            of Radisson SAS is quite clear about the challenges when he says: 
            “There is room for little more growth in the occupancies. There is 
            still the scope to work out something for the summer time. Leisure 
            tourism is one area we need to focus on in the future. We can 
            certainly drive more business in the low season (summer time).” 
            According to Brandes, everybody is concentrating on only the beach 
            resorts in Oman at the moment. “What’s missing here is stunning 
            desert resort, or something stunning in the mountains.”<br>
            <br>
            The other challenge, says Brandes, is the drop in occupancies in the 
            summer months. “Last June, we had 42 per cent occupancy, in July 30 
            per cent, and in August 45 per cent. But one cannot drop the rate 
            dramatically. I think it is just too important to cover your cost.”<br>
            <br>
            Cavagliotti says that the InterContinental is going to launch 
            several different packages for the lean months. But he agrees it 
            will probably take some time to change the perception. “People are 
            under the impression that there is no way that they can come to Oman 
            in summers. This shouldn’t be the case as Dubai has managed quite 
            well to reverse the trend. With time, I believe we can achieve this 
            in Oman also. But then, it is not something that can be done 
            overnight.” <br>
            <br>
            Maclean puts the challenges in a wider perspective. “With 
            occupancies going up, the pressure on operations has increased too. 
            Room rates have trebled from what they were two years ago. So, a 
            customer is now paying three times the rate. As a corollary to it, 
            he now nurtures three times the expectations in terms of service and 
            facilities. But the young person, who is delivering it, has not been 
            trained for three times the duration. And he is not being paid three 
            times the salary. So, there is a bit of a gap in expectation. And, 
            we’ve got less time to produce a higher level of skill sets. This, 
            really, is a challenge.”<br>
            <br>
            The challenge for a training institute like NHI is that it is now 
            dealing with some very high-standard hotels that are charging their 
            clients a lot of money. “Therefore, they expect the skills of our 
            people to be at a much higher level than earlier. But we get only a 
            fixed period of time to train them. And we do as much as possible 
            within that time,” adds Maclean.<br>
            <br>
            How ready is the trainee for the market? According to Maclean, they 
            (trainees) are 70 per cent ready. They have got over another 
            three-four months to become 100 per cent ready, which is supposed to 
            happen at their workplace.<br>
            <br>
            Are there any challenges on the demand-supply front? On the supply 
            side, Maclean does not see a challenge. “We have managed to satisfy 
            the needs of most of the hotels for the last couple of years. Even 
            as we had Shangri-La coming in, we were still able to provide 
            trainees to most of the other hotels.”<br>
            <br>
            Maclean, however, sees some challenges on another front. He feels 
            that with this new move on expatriates not requiring No Objection 
            Certificates to move around, there are bound to be some challenges 
            there. <br>
            <br>
            He guesses that the hospitality industry is now going to see some 
            more normal working environment (read hours). “We all accept that 
            the hospitality industry is horrific when it comes to working hours 
            but things are changing for the better. And this will make it an 
            industry of choice for workers.” <br>
            <br>
            Maclean, however, feels one of the major benefits of the boom in the 
            hospitality industry that it will pave the way for greater 
            Omanization. “I think any employer will say the same.” Some of the 
            major hospitality entities see construction work next to the hotel 
            as a major challenge and concern for the year 2006. Some of them 
            have had to cancel room nights, reflecting, thereby, a significant 
            loss. The increase in the small hotels and apartments, which sell 
            the rooms and other services at very low rates are major concerns to 
            some of the major hotels. <br>
            <b><br>
            The Great Debate: Private versus Public</b><br>
            According to a dominant view, one of the major challenges facing the 
            hospitality industry in Oman rests in the “private-versus-public” 
            domain. Two of the major hotels in the city of Muscat — 
            InterContinental and Al Bustan Palace — are government-owned. Then, 
            some of the other big ones — like The Chedi and Barr Al Jissah — 
            also have significant stake of the government. While Barr Al Jissah 
            has reportedly 40 per cent government stake, The Chedi is mainly 
            owned by the Government Pension Funds. Grand Hyatt is probably the 
            only big player in Muscat that is fully owned by the private hand.
            <br>
            <br>
            Grand Hyatt is doing exceedingly well. Says Ahmed, the hotels’ GM: 
            “By and large, it’s paramount that the government plays a regulatory 
            role in it (hospitality industry) rather than having a deep 
            involvement. When you are a private enterprise employee, you’ll make 
            a much bigger effort to get the business in, and to look after it. I 
            think that in the service industry, although it’s very desirable for 
            the government to get involved in the initial stages, as we move 
            forward, private enterprise ought to be left to thrive and survive 
            on its own and make its own mark.”<br>
            <br>
            Brandes of The Chedi echoes a similar view. “I agree that they (the 
            hotels) should all be private.”<br>
            <br>
            Says Maclean: “It’s interesting to see that one of the most 
            successful hotels at the moment is the Grand Hyatt. It’s private. 
            Crowne Plaza is doing extremely well and it’s private. And the 
            government is actually selling the Golden Tulip Seeb (located near 
            the Seeb Airport). I think it is a good move. I think the government 
            has realised that it should not really be in the hotel business. 
            Yes, there is a responsibility to get things going but it should 
            withdraw.” <br>
            Of Shangri-La, Maclean says it would be interesting to see whether 
            the government retains its share or not. “I have always said that 
            Shangri-La is like a watershed. It is like unlocking the dam.”<br>
            <br>
            Maclean adds: “Okay, there are some exceptions. There is Al Bustan. 
            There is Intercontinental. Bustan has always been an icon for the 
            government. But there is talk of the Intercontinental going 
            private.” <br>
            <br>
            However, Zamora of Al Bustan does not think that ownership has 
            anything to do with operating a resort or hotel. “This hotel (Al 
            Bustan) does the business it does because it is the kind of hotel it 
            is. A tourist never says he wants to go to Al Bustan because it is 
            owned by the government. There may be some business coming to the 
            hotel because it is owned by the government but it is not the reason 
            for its success now. The other hotels that are not governed by the 
            government are performing equally well.”<br>
            <br>
            However, even Zamora is not sure if it is in a government’s interest 
            to be long-term shareholder in any industry. “I guess in the long 
            term the government will sell its equity in the hospitality industry 
            and allow the market to be totally in the hands of the private 
            sector.” <br>
            It cost around half a billion dollar in 1985 to build the Al Bustan 
            Palace. A private company — probably — wouldn’t have spent this kind 
            of money to set up a hotel. But while it is all right for a 
            government-backed hotel to have consistently high occupancy, when 
            there are such high investments, the break-even period is quite 
            long. So, until the break-even point is achieved, even higher 
            occupancy fails to cut losses. And it is here that the logic of 
            private ownership for the hospitality industry makes more sense. 
            For, a private enterprise is – usually – more cost-conscious, and 
            works to achieve the break-even with greater passion. </font></p>
