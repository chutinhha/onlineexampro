            <p align="justify"><font face="Verdana" size="1"><b>The Boom Factors: Why Now?</b><br>
            Why did the boom happen now? After all, what suddenly changed in a 
            couple of years? The top executives of Oman’s hotels say that the 
            Omani government’s increasingly effective marketing and promotion 
            strategies abroad, coupled with the demand pressures in Dubai, are 
            really working wonders. <br>
            <br>
            The hospitality industry in the Middle East (ME) was down in the 
            dumps following 9/11. Tourism in ME felt the direct impact of the 
            World Trade Centre crash. Therefore, few could have visualised this 
            boom coming within a few years later. Robert Maclean, Principal of National 
            Hospitality Institute (NHI), feels terrorism does not have that big 
            an impact on tourism as a large part of the globe is affected by it 
            and people would ultimately like to go where they want. “I think the 
            publicity and the perception of Dubai has created a better 
            environment in this region than it would have had done normally.”
            <br>
            <br>
            In fact, the Ministry of Tourism has been burning the proverbial 
            midnight oil to attract tourists for a long time now. Says Zamora of 
            Al Bustan: “The efforts of the Ministry of Tourism over the last 
            10-15 years are beginning to pay off now.”<br>
            <br>
            The Omani Government, in 2005, created Ministry of Tourism as an 
            independent ministry, a step highly valuable for the tourism sector 
            in the Sultanate.<br>
            <br>
            Tourism, says Hyatt’s Ahmed, depends on ease of travel, connections 
            and arrival. “Oman government’s lifting of the visa restrictions and 
            making it easier for people to get visas on arrival, was the 
            starting point for Muscat taking off as a major tourist 
            destination.” <br>
            <br>
            According to Pike of Radisson SAS, the setting up of the Ministry of 
            Tourism has been a very positive move in the promotion of Oman 
            tourism. One of the key steps that the ministry took was to employ 
            market-based representatives in the key feeder markets in Europe, 
            Asia etc. These market-based representatives are filling the need, 
            which was perhaps missing in the past. Awareness of the consumer is 
            the key in feeder markets. <br>
            <br>
            Pike of Radisson says: “Tour operators are now coming to the 
            hoteliers of Oman asking for rooms, rather than us chasing them. The 
            interest from international tour operators in Oman is certainly 
            increasing. They see it as a destination for the future. I certainly 
            see Oman as the destination of the future.” <br>
            <br>
            NHI’s Maclean agrees that Oman is now being better marketed, 
            particularly in the UK where it’s very much being seen as the 
            slightly mystical and magical. “I think that is an attraction. 
            People are getting a bit fed up of some of the traditional 
            Mediterranean locations. Oman is in the niche category. A large 
            number of people are coming in from Europe, and at quite expensive 
            rates too.”<br>
            <br>
            It is no small coincidence that this boom in tourism traffic comes 
            at a time when the Sultanate’s economy is booming and the upcoming 
            industrial projects in Sohar are all set to catapult the Sultanate 
            to a new pedestal in the GCC and global fraternity. <br>
            <br>
            Zamora of Al Bustan Palace feels the Tsunami had shut a substantial 
            number of tourist destinations in the Far East. “That traffic had to 
            go somewhere. Some went to the Bahamas and the Caribbean, but some 
            came here (to Oman),” he says. <br>
            <b><br>
            Boom: How Real? </b><br>
            It would be prudent, however, to gauge how real this hospitality 
            industry boom is. After all, the surge in room rates may not 
            continue for that long, and may plateau out, sooner than expected. 
            Says Brandes of Chedi: “There is still a little bit space for 
            further increase in room rates but we have to be careful that we do 
            not shoot ourselves off the mark. As long as you are reasonable, you 
            can deliver.”<br>
            And, what happens when the mega tourism and residential development 
            projects like The Wave (expected to be completed by the end of 
            2007), Blue City, and at least seven other projects - in various 
            stages of discussion/finalisation – get ready? “Five years from now, 
            there will be a lot of availability but the demand may not be as 
            much,” agrees Brandes. “So we have to be more careful from both the 
            sides,” he adds.<br>
            <br>
            Pike of Radisson SAS echoes similar sentiment: “Hotels are all 
            enjoying high occupancies now and the rates are continuing to grow. 
            But for how long they will continue to grow is something I cannot 
            say at the moment. I think the rates will plateau out a little bit. 
            They have already started to. ”</font></p>
