            <p align="justify"><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">ourists 
            heading for the pristine shores of Oman this year might have to 
            carry a slightly heavier purse. They may well need to shell out a 
            little extra on hotel room rates. In fact, rates have already been 
            following an upward trend over the last year. The ascendance only 
            continues. And, why not? The hotel industry is booming right now.
            <br>
            <br>
            <img border="1" src="cover1.jpg" width="200" height="261" align="left" hspace="5" vspace="2">High occupancies, increasing rates, heightened interest of 
            globetrotters, unveiling of new projects — it’s a wide spectrum of 
            sunny indicators for the hospitality industry in the Sultanate right 
            now, and Muscat in particular. No wonder, hotels are bursting at the 
            seams when it comes to occupancy. An increasing interest in the 
            Sultanate as a leisure destination has changed the dynamics of what 
            was once perceived as a static, oversupplied market.<br>
            <br>
            2005 saw Oman making its mark as a top brand tourist destination 
            with European leisure guests, GCC business travellers, MICE 
            (meetings, incentives, conferences and exhibitions) business guests. 
            All of these are swelling in numbers as the months pass by.<br>
            <br>
            “Oman is a hot destination right now,” says A. Antonio Zamora, 
            Director of Operations, Al Bustan Palace, the government-run hotel 
            located 8 km from the downtown business district in Muscat, and 
            surrounded by a dramatic mountain backdrop, of sea and beach. “It 
            (Oman) has become the destination of choice for the whole of 
            Europe,” Zamora exclaims and adds: “Efforts are being made now to 
            attract tourists from countries like the United States.” <br>
            <br>
            According to the HotelBenchmark Survey by Deloitte, the Middle East 
            region, with an impressive 21 per cent growth in revenue per 
            available room (revPAR), was for the second year in a row, the best 
            performing region in the world during 2005 (See Box: Middle East 
            Shining). As per the survey, hotels in Oman took the third prize – a 
            no mean achievement - with revPAR growth of 54 per cent. The 
            HotelBenchmark Survey contains the largest independent source of 
            hotel performance data outside of North America and tracks the 
            performance of over 6,500 hotels and 1.2 million rooms every month.<br>
            <b><br>
            Swelling Occupancy</b><br>
            <b>
            <a href="JavaScript:NewWindow('new1.htm')">
            <img border="0" src="cover03.jpg" width="250" height="249" align="right" hspace="5" vspace="2"></a></b>As 
            for the boom in Oman, one just needs to have a look at some of the 
            figures achieved by the major hotels. Grand Hyatt, Muscat had 
            averaged 75 per cent occupancy in 2004. But it (occupancy) jumped to 
            85 per cent in 2005. And to add to it, the first two months of 2006 
            have been excellent. The growth witnessed in 2004 and 2005 continues 
            this year as well.<br>
            <br>
            Radisson SAS Hotel, Muscat finished with an occupancy rate of around 
            95 per cent in 2005. In the preceding year, it had registered a mere 
            78 per cent occupancy. As for 2006, says Christopher Pike, General 
            Manager: “It continues to be very strong. And the rates are 
            continuing to grow.”<br>
            <br>
            For The Chedi, Muscat, there was a big leap in occupancy in 2005, 
            compared to 2004, says York Brandes, the premier hotel’s General 
            Manager. “The jump was approximately to the tune of 25-30 per cent. 
            We had a slow beginning in 2004 and a very strong winter season. 
            Occupancy remained high even in Ramadan period (when it usually 
            takes a beating). We had a very good start in 2005, which continues 
            till date,” Brandes claims.<br>
            <br>
            Zamora, the GM of Al Bustan Palace (part of InterContinental Hotels 
            Group), considers 2005 a “spectacular year” for the Omani 
            hospitality industry. “We did very well despite the fact that we do 
            not have commercial travellers here at Al Bustan. What we need in 
            Muscat right now is more hotel rooms.”<br>
            <br>
            Golden Tulip Seeb was reported to have registered 76 per cent 
            occupancy in 2005 compared to 67 per cent in 2004. Holiday Inn 
            Muscat reportedly saw its occupancy in 2005 shooting up to 74.32 per 
            cent from 67.28 per cent in the preceding year. <br>
            <br>
            <a href="JavaScript:NewWindow('new2.htm')">
            <img border="0" src="cover04.jpg" width="200" height="134" align="left" hspace="5" vspace="2"></a>Intercontinental, 
            Muscat was no different. Says Renzo M. Cavagliotti, the 
            government-run hotel’s General Manager: “2005 was actually a very 
            good year for us. The occupancy went up to around 72.3 per cent in 
            2005, compared to 69.8 per cent in 2004. And the best part was, more 
            than the occupancy, the room rates registered a 35 per cent 
            increase, which is quite extraordinary for any country.” And 
            Cavagliotti is further buoyed by the fact that the rate has 
            registered another 15 per cent hike in January and February this 
            year.<br>
            <br>
            Room Rates: Towards the Roof Cavagliotti is not the only one 
            surprised by the surge in room rates. If one considers that it was 
            not in the too distant past when rate cuts were often employed by 
            the hotel industry, in general, to attract traffic, it is truly 
            astonishing. <br>
            <br>
            Pike of Radisson SAS says: “In the last one and a half years, the 
            average room rates have inched towards a more realistic level. In 
            the past, the demand was obviously not as strong as it is now. The 
            demand is shooting up and so the opportunities are there to push the 
            room rates up to a level where it reflects the true value of the 
            hotels, the products, and the services offered by us.” <br>
            <br>
            Brandes, Chedi’s GM, is more categorical as he declares: “I can 
            guarantee that all of us will increase the room rates.” His hotel 
            has seen the rates grow approximately between 8 and 10 per cent 
            between 2004 and 2005. “ When we first pushed the rate up, people 
            were saying we wouldn’t be able to manage it. Now, look at the 
            figures of the last two years! We have been the driving force behind 
            driving up the room rates of the hotel industry here. Our hotel 
            keeps full even at the much higher rate,” he declares. <br>
            <br>
            Al Bustan’s Zamora puts the expectations into figures: “We are again 
            looking at a double-digit hike in rates this year.” The hotel had 
            already registered double-digit hikes in both 2004 and 2005.<br>
            <br>
            Munir Ahmed, General Manager of Grand Hyatt Muscat, a private-run 
            premier hotel, feels the move to hike rates had also to do with the 
            Dubai example. “People saw what had happened in Dubai. The rates had 
            gone very high. Oman, to an extent, only followed suit and rates 
            went up by 30-45 per cent. Earlier, the rates were very low in Oman. 
            In fact, the 22 Arab countries had one of the lowest rates. Oman is 
            now is in the big league but is still very good value for money. The 
            rates here are still far below compared to some of the neighbouring 
            countries.”</font></p>
