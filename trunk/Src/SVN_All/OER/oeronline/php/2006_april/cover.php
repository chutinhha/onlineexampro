            <b><br>
            Who is coming: A profile</b><br>
            When it comes to the profile of the tourists flocking to Oman, it is 
            probably the Europeans, whose number is the highest, especially 
            those coming from the UK and Germany.<br>
            <br>
            However, Pike of Radisson SAS is convinced that the profile of 
            visitors is across the board. He says it is a similar nature of 
            guests who have been visiting Oman in the past; only their numbers 
            have swelled now.<br>
            <br>
            Brandes of The Chedi confirms that most of the tourists coming to 
            Oman are from Europe. “We also have visitors from Asia but there are 
            hardly any Americans. The trend will stay like this.”<br>
