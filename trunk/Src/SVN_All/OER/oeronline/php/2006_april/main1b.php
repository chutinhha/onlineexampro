<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><br>
<b>
<font face="Verdana" style="font-size: 14pt">Sohar Port will benefit us greatly<br>
</font><font face="Verdana" style="font-size: 8pt">An exclusive interview with R 
Premchandran, General Manager, Al Khaleej Polypropylene, Sohar</font></b></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>When did you decide to set up a manufacturing 
base at Sohar? </b><br>
The decision to set up our base in Sohar was taken in June 1996 by the founding 
members of Al Khaleej Polypropylene Products Co. SAOG. <br>
<b><br>
What were the reasons behind this move?</b><br>
The main reason was the locational advantage. The availability of land at fair 
lease rate and the tax holiday were very crucial factors.<br>
<b><br>
Is it an additional capacity building exercise?</b><br>
No. It is a Greenfield project.<br>
<b><br>
When the Port Sohar is completed, will it help you to expand your own market? 
Which are the overseas markets you are eyeing?</b><br>
The majority stakes in the company has been recently bought over by Dubai Poly 
Films. This firm is based in Dubai and belongs to the Al Ghurair Group. The 
current target of sales from the Oman operation is based on exports to the rest 
of the world apart from the GCC, Middle East and North Africa markets, which 
will be handled by the remaining constituent companies of the group. Sohar port 
offers container-handling facility. Then, there is the advantage of frequent 
service from major shipping lines that will call at this port. It would benefit 
us greatly.<br>
<b><br>
With the opening up of the Omani economy and the recent signing of FTA with 
United States, the entire world is becoming a market place for manufacturers 
across the world. What is your company’s global vision?</b><br>
The goal of the parent company, which is under formation, is to become a global 
player in the field of manufacturing of BOPP films.<br>
<b><br>
Given the technological dependence of your operations, what is the level of 
Omanisation at your workplace? </b><br>
The current Omanisation percentage stands at 53.<br>
<b><br>
Waste treatment is one of the major sources of corporate social responsibility 
these days. What are your company’s plans and experiences in this arena?</b><br>
The company recycles all plastic waste produced in the plant, including the 
waste metalised films. The company has invested in sophisticated waste recycling 
machines to support the philosophy of recycling of waste.<br>
<b><br>
What is the size of your workforce at Sohar? </b><br>
The total head count in the company currently stands at 166. We have 88 Omani 
employees and 78 expatriate staff.<br>
<b><br>
What is your forecast for the polypropylene industry in Oman and at the global 
level?</b><br>
The growth rate in the region stands at 9-10 per cent and the global average is 
predicted to be in the region of 7-8 per cent.<br>
<b><br>
How tough is the competition from GCC countries? What steps is your company 
taking to take the competition head on?</b><br>
Following the recent takeover of Al Khaleej Polypropylene Co. by Dubai Poly 
Films, the competition in the region from GCC countries has been curtailed. The 
company is <br>
focused on product diversification and market diversification as a continuous 
ongoing development to avoid competition.<br>
