</font><font face="Verdana" style="font-size: 14pt">Engineers have a huge social 
responsibility</font></b><font face="Verdana" size="1"><b><br>
An interview with 
Bryan James Westcott, the National President of South African Federation of 
Civil Engineering Contractors (SAFCEC)</b></font></p>
