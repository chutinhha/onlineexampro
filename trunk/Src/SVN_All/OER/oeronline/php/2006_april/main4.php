<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he 
Bahrain-United States FTA seems to be stuck in a logjam. The issue of 
intellectual property rights (IPR) is reportedly causing the delay in the 
implementation of the free trade agreement (FTA) between Bahrain and the US, 
which, ironically, could still be weeks away, despite the US President George W. 
Bush signing the document into law in the first half of January 2006. <br>
<br>
This FTA was signed as early as in September 2004, and Bahrain’s Parliament 
endorsed the proposal in July last and won the Congressional support in late 
2005. <br>
<br>
Says Dr Jasim Husain Ali, Head, Economic Research Unit, University of Bahrain: 
“This delay is because the Bahraini authorities must first legislate laws 
extending protection of IPRs.” The IPRs deal with protecting all matters related 
to intellectual property, including software, CDs, copyrights etc. <br>
<br>
“The fact is, approval, rather than negotiations, proved to be the most 
difficult element of the FTA between the countries,” says Ali, one of the 
renowned economic commentators in the Kingdom of Bahrain. Bilateral negotiations 
lasted merely five months culminating in the signing of the treaty in September 
2004. However, no date was fixed for implementing the deal. What is left — 
though not stated publicly to avoid any sort of embarrassment — is that the 
Bahraini side must enact legislations, providing protection for IPRs and 
proposing specific penalties for violators.<br>
<br>
Reports from Bahrain suggest that the government has come up with a draft 
proposal regarding IPRs and it is up to the Bahraini Parliament to legislate the 
proposal, together with the penalties for violators. “Besides, Bahraini 
authorities have committed themselves to upgrading all labour laws by bringing 
them in line with the requirements of International Labour Organisation (ILO),” 
informs the Bahraini academic. “To be sure, this is not to be conditional for 
the implementation of FTA. The topic came to light during the Congressional 
discussions of Bahrain’s labour laws. Hence, the Bahraini side unilaterally 
agreed to bring its labour laws in accordance with the ILO’s standards,” adds 
he.<br>
<br>
Bahrain is eagerly awaiting the implementation of FTA for obvious reasons. The 
deal would expose Bahraini goods and services to the world’s largest consumer 
market. The US imports are valued at around US$1,200 billion annually. By 
comparison, Bahrain’s import amounts to about $7 billion per annum.<br>
<br>
The two-way trade between Bahrain and the US amounted to $706 million in 2004. 
According to the US Census Bureau, Bahrain had a surplus of $104 million by 
virtue of exporting $405 million worth of goods and services. Yet, in 2003, 
bilateral trade amounted to $886 million with the US enjoying a surplus $130 
million on the back of exporting $508 million worth of goods and services to 
Bahrain. The decline in trade value is attributed to drop in sales of big-ticket 
items such as aircraft. <br>
<br>
Once the FTA comes into effect, an average of 97 per cent of industrial and 
agricultural products will immediately have the duty-free access to each other’s 
market, with the balance of the restrictions to be phased out in a span of 10 
years. <br>
<br>
The FTA with Bahrain is part of US President George W. Bush’s goal of creating a 
Middle East Free Trade Area (MEFTA) by 2013. Thus far, the US has signed 
agreements with Jordan, Morocco, Bahrain, and more recently, Oman. The UAE is 
next in line but talks on FTA seem to have been hit by the dust kicked by the DP 
World controversy. Says Ali: “At any rate, it seems that the American side is 
not prepared to implement the FTA with Bahrain — or any other country — prior to 
obtaining commitment regarding the availability of specific legislation dealing 
with intellectual property.” </font></p>

