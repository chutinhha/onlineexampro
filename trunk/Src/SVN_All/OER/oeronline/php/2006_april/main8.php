<p align="justify" dir="ltr"><font face="Verdana" size="1">
<br>
<br>
</font><b><font face="Verdana" style="font-size: 14pt">Go for sound HR practices</font><font face="Verdana" style="font-size: 8pt"><br>
G.S. Nair (Kumar), Head of Beverages with Oman United Agencies, explains how, by 
empowering employees and giving them the opportunity to learn while they earn, a 
company can foster growth</font></b><font face="Verdana" size="1"><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="lastpage.jpg" width="134" height="207" align="left">I</b></font><font face="Verdana" size="1"> 
am the first Asian to be promoted to head the beverages business at my company. 
When I was promoted to my current position, I got calls from my colleagues in 
Bahrain, Qatar and the UAE, congratulating me, and expressing the hope, that 
once I move higher, they could have a chance to step into my shoes.<br>
<br>
I see this as a great opportunity. My growth in the company has been slow and 
steady over the years. My elevation has indeed boosted the morale and confidence 
of the Asians working in the company. They are now more hopeful of growth. <br>
<br>
It surely will prompt them to give their best, thus preventing lateral shifts 
within the industry. <br>
I joined Oman United Agencies (OUA) in 1990. Prior to that I worked in India 
with Hindustan Pencils and United Engineering in their accounts management. I 
joined OUA as a Retail Assistant in one of the company’s retail outlets and was 
promoted to the rank of Shop Manager in the prestigious Madinat Qaboos outlet. A 
couple of years later, I became the Retail Shop Supervisor, in-charge of five 
retail outlets in the city, in-charge of marketing the principal brands and 
event management. Thereafter, I moved into logistics and looked after OUA’s 
inbound and duty free business, catering mainly for the diplomatic and 
expatriate customers. <br>
<br>
Four years back, I took charge of the on-trade business, which mainly looks 
after the supply chain of the company dealing with hotels and restaurants. In 
December 2004, I was promoted to head the beverages business. <br>
<br>
In the meantime, the company had also taken over ONAS Trading that deals in the 
relatively down-market beverages brands. Recently, QUA has down-streamed into 
its first restaurant business called ‘Uptown Restaurant.’ It caters to a wide 
variety of clients — right from bachelors to families. Now, we are planning to 
extend it into a chain.<br>
<b><br>
Teamwork, the key to success </b><br>
I have always believed that formal education gives one a sense of direction. 
Ultimately, when it comes to one’s profession, one has to rely on people. 
Empower them. Give them the freedom — even the freedom to make mistakes. Only 
then will they learn and give a handsome return on the investments that the 
company makes on them. <br>
<br>
Whatever I have achieved is because of the people I have worked with. I have 
empowered them to take decisions. I have intervened only when they have asked me 
to. And since I have been in various functions within the company, I could 
easily identify myself with the problems that my staff may have faced. <br>
<br>
Over the past 14 years that I have been in Oman, I have seen the market grow 
from a quiet place to an aggressive one. We can no longer take business as it 
comes. With the booming tourist business in the Sultanate, all the players in 
the liquor business have been forced to sit up and take aggressive business 
decisions. And, with growing investments in this sector, like Shangri La, our 
confidence in growing business opportunities only gets reinforced. <br>
<b><br>
Win over the staff</b><br>
The biggest challenge for me is to achieve a smooth blend of the various 
cultures as we have employees hailing from all corners of the world. If we are 
able to manage this blend efficiently, we can bring out the best in the 
employees. Many a time, what an employee wants is a little pat on the back, and 
not some financial compensation. Such gestures essentially mean appreciation and 
recognition of effort. This philosophy of people management is rewarding in the 
long-term, because we earn the loyalty of the staff. This saves a lot of 
training cost of the company.<br>
<br>
In this market, one hallmark of the people you deal with — be they customers or 
people within the company — is their affable nature. Work and business are 
important, but harshness is not. Business ought to be done affably. This is what 
I learnt in the local market. <br>
<br>
As a large player in this business, we have outperformed the market. The key to 
the company’s success is the trust it has placed in its employees and rewarded 
them justly. In the end, it is all about a little mental comfort. When one has 
that, one can move mountains and not seem to mind. <br>
<i><br>
As told to Aparajita Mukherjee</i></font></p>

