<p align="justify" dir="ltr"><font face="Verdana" size="1">
<br>
<br>
</font><b><font face="Verdana" style="font-size: 14pt">Smashing the stereotype</font></b><font face="Verdana" size="3"><b><br>
</b></font><font face="Verdana" size="1"><b>Like 
all fields in life, golf has been no exception as far as creating a glass 
ceiling for women is concerned. But women like Annika Sorenstam believe in 
breaking the barriers, carving out a niche for themselves courtesy merit and 
hard work<br>
</b><br>
<b>
<img border="0" src="golf_update1.jpg" align="left" width="150" height="208" hspace="5" vspace="2"></b></font><font face="Verdana" style="font-size: 11pt"><b>I</b></font><font face="Verdana" size="1">n 
fact, golf heritage can neatly be divided into two eras — everything that 
happened before Annika Sorenstam arrived on the scene, and everything that has 
happened since. Such has been the impact of Annika. She has single-handedly 
shattered all the stereotypes of the overly mechanical, utterly emotional sport.
<br>
<br>
As her debut on the Professional Golfer’s Association (PGA) Tour demonstrated, 
golf’s future just might be a battle of the sexes for supremacy. A pivotal point 
in the history of world golf occurred during the controversial decision of 
inviting Annika to participate in the Colonial Golf Tournament, Fort Worth, 
Texas in 2003. She was the first woman player to play in PGA event. She finished 
95th out of the total 111 participants amid criticism from her male colleagues 
who felt she could not make the cut.<br>
<br>
Annika’s participation fueled much discussion and debate on whether her 
induction was a mere novelty or truly signaled a new era in women’s golf and 
golf per se. There were differences of opinion between women commentators 
themselves, with some women athletes supporting Annika and claiming that her 
presence drew attention towards those woman athletes who were usually compelled 
to play in the shadows of their more acclaimed male peers. There were others who 
argued that golf should not be played competitively between the sexes in 
recognition of the innate physical differences. <br>
<b><br>
Consistent cut</b><br>
While such debate carries on, Annika has gone on to establish herself as the 
greatest female golfer of all time, combining supreme skill and a unique style 
of her own. Her consistency with the clubs is awe-inspiring and has been that 
way for the past 10 odd years. She is among the longest hitters on the LPGA 
Tour, while also being the most accurate. <br>
<br>
She is the first non-American to be a member of the LPGA Hall of Fame. She made 
her debut as a full-time member of the LPGA Tour in February 1994 and has not 
looked back since then. At the end of 2003, Annika added to her growing list of 
accolades. Voted “Associated Press Female Athlete of the Year”, Annika was named 
the “Golfer of the Year” in Sweden for the third year in a row and seventh time 
in her career. In addition, Sweden also awarded her with the “Jerring Prize”, 
one of the most prestigious Swedish sports awards. She was also honored with the 
title of “Female Athlete of the Year”. <br>
<br>
The winner of 66 LPGA tournaments and nine major titles to date, Annika is the 
holder of numerous LPGA records, including the lowest round in LPGA history and 
the only player to win the same event for five consecutive years (Mizuno Classic 
2001-2005). In fact, Annika has more tournament wins than any other golfer (yes, 
even more than Tiger Woods!) and is the only LPGA player to have appeared on a 
postage stamp. She is the only wo<b><img border="0" src="golf.jpg" align="right"></b>man golfer to have won the Grand Slam in 2004.
<br>
<b><br>
Perfect mechanics</b><br>
It’s hard to pinpoint Annika’s greatest strength on the course. She’s a fabulous 
iron player. Her mechanics are near perfect. Her tempo and rhythm are simply 
superb. This allows her to strike the ball with amazing consistency. She is as 
mentally strong as anyone in golf, man or woman. <br>
<br>
She pens reminders and short inspirational notes on the bill of her cap, falling 
back on them when the pressure of a tournament intensifies. Many have tabbed her 
as being totally devoid of emotion. In reality, she is often the victim of 
nerves. She has, over time, learnt to control these feelings. That’s partly 
because she prepares so meticulously for every tournament she enters. And, few 
things on a golf course ever surprise her.</font></p>

