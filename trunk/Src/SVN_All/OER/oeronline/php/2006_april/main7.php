<p align="left" dir="ltr" style="margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" style="font-size: 14pt">Killing Your Best Ideas 
with a Lame Presentation<br>
</font><font face="Verdana" style="font-size: 8pt">Poor presentations are the 
ones that typically wallow in details better left at the preparation stage, says 
David Batstone</font></b></p>
<p align="justify" dir="ltr" style="margin-right: 10px">
<font face="Verdana" style="font-size: 8pt"><b>
<img border="1" src="mang.jpg" width="117" height="191" align="left"></b></font><font face="Verdana" style="font-size: 11pt"><b>S</b></font><font face="Verdana" size="1">ome 
of the most creative ideas, indeed the most promising business plans, never see 
the light of day due to poor presentation. Recently, I witnessed a “murder” by 
inept delivery. A services company was pitching the key decision makers at a 
transnational company on what was truly a dynamic leadership development 
program. Rather than jump straight to the point, and describe what they would do 
and how it would result in better company leaders, the pitchmen got lost. <br>
<br>
They opened with a PowerPoint presentation, and took 25 minutes to review 
general “leadership theory” that lay at the foundation of their leadership 
program. Honestly, their lesson on leadership theory offered concepts that 
reasonably well-informed managers&nbsp; already accepted. No surprise, the 
decision makers soon got bored, looked at their Blackberry messages, and had 
that look of a prisoner plotting an escape. By the time the pitchmen got to the 
exciting part of their program, malaise already had set in.<br>
<b><br>
Don’t Deliberate on Process</b><br>
The fatal detour the pitchmen took is altogether too common. When you give a 
pitch, do not share with your audience your deliberation process. Poor 
presentations typically wallow in details that should be left at the preparation 
phase. Get to the sizzle of your offering as quickly as possible. Then move 
backwards (when relevant) to tease out the assumptions that led you to your 
design. These matters of method best emerge in a discussion period following 
your presentation.<br>
<br>
The cardinal sin of presentation is to place the key ideas of your pitch on a 
PowerPoint, then simply read off the screen. I hesitate to even mention this 
obvious gaffe, but I sit through so many presentations that follow this path to 
oblivion that it bears mentioning. A media presentation should in some way 
augment or illustrate what you are presenting in your spoken delivery. <br>
<br>
A meaningful data chart, for example, might supplement an idea. If something you 
throw up on screen simply repeats what you are saying, drop it.<br>
<b><br>
No Generalities, Please!</b><br>
Many presentations kill audience attention with generalities. When I was working 
in&nbsp; the investment banking world, I read a&nbsp; steady diet of executive 
summaries and sat through countless pitch presentations from companies in search 
of investment funding. I always got a good chuckle out of the executive 
summaries that would note a target&nbsp; audience of 220 million for their 
product in the United States. <br>
<br>
That’s just about every breathing man, woman, and child in the country, of 
course. In a similar vein, if 50 million people in the USA are actively using 
the Internet, it is unrealistic to expect 49 million as your target audience. 
The more specific and tailored&nbsp; your pitch, including your data, the more 
credibility it will carry. Get across as plainly as you can to your potential 
client or investor why your offering will make a difference&nbsp; for their 
niche.<br>
<br>
<img border="0" src="mang1.jpg" width="250" height="209" align="right">Ok, why 
don’t I take my own medicine on being specific! Looping back to my original case 
study, the pitchmen for the leadership-training program would have been well 
served to find out as much about the challenges facing the transnational company 
that they were pitching. What do employee surveys say about leadership in the 
company? What major challenges is the market presenting&nbsp; the company; that 
is, what competitors are&nbsp; on the horizon? What transitions — say,&nbsp; 
expanding into several new global markets&nbsp; or scaling back a product line — 
is the company facing at the moment?<br>
<b><br>
How to Become Compelling</b><br>
The more closely their presentation can address those challenges, and explain 
how their services will lead to positive change, the more compelling they will 
become. <br>
Waffling in the realm of general leadership theory, on the other hand, yields 
glazed <br>
eyeballs. A strong presentation can make even a bad idea seem plausible. Imagine 
what the right presentation can do for your most innovative ideas!<br>
<br>
<i>David Batstone is the author of Saving the Corporate Soul and teaches 
business ethics at University of San Francisco, United States.</i></font></p>

