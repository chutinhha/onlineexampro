<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>E</b></font><font face="Verdana" size="1">ngin 
Turker could not have begun his tenure as the Ambassador of the Republic of 
Turkey to the Sultanate of Oman at a more opportune time. Trade ties between the 
two countries have gone from strength to strength over the past few months. And 
many developments slated for the future will take the bilateral ties to a new 
high. <br>
<br>
While Turker joined the Embassy a few months ago, the first-ever Turkish 
products exhibition was being organised in Oman. A Memorandum of Understanding 
was signed between the Istanbul Chamber of Commerce and the Oman Chamber of 
Commerce and Industry (OCCI), business deals worth $20 million were concluded, 
with the participation of as many as 56 Turkish companies. Then, the OCCI agreed 
to organise an Omani exhibition in Istanbul, in cooperation with Istanbul 
Chamber of Commerce, later this year. <br>
<br>
To cap it all, Turkish Airlines launched direct flights between Istanbul and 
Muscat in March with a view to further boosting relations between the Sultanate 
and Turkey. For more convenience of the passengers, non-stop flights between 
Istanbul and Muscat will be launched from June. As a link between the East and 
the West, this provides the best opportunity to promote Oman as a tourist 
destination and promote Omani products, not only in Turkey, but in the entire 
European market as well.<br>
<br>
There are more agreements in the pipeline. On the cards are cooperation between 
the Archives of the two countries, a maritime agreement, cooperation agreement 
on tourism, agreement on cooperation on legal matters in the fields of commerce, 
penal code and consular affairs, an agreement on avoiding double taxation 
(already completed, it only remains to be signed), and an agreement on 
reciprocal promotion and protection of investments (almost completed, to be 
signed).<br>
<b><br>
Sky, the Limit </b><br>
Turker has an enviable task up his sleeves in the coming times in view of the 
promising developments in transport and communications. “Turkish Airlines’ 
direct flights (weekly) between Istanbul and Muscat will give a big boost to 
tourism in both countries. It will also enhance our commercial, cultural and 
business relations,” the Ambassador points out. <br>
He also has a “special formula” on the tourism horizon. “Turkish visitors are 
likely to flock to Oman during the winter season and Omanis will do the same in 
their summers.” Turker nurses no doubts that this is surely a winning formula. 
“Turkey and Oman could cooperate and benefit from these advantages.”<br>
<br>
It is no coincidence that both Turkey and Oman are favourite destinations for 
tourists. Last year, about 20 million tourists visited Turkey. Oman would be an 
attractive destination for winter tourists, who would like to enjoy sunshine 
while their countries are freezing.<br>
<b><br>
Business Ties: The Surge</b><br>
Since 2000, Turkish contracting and consultant companies have successfully 
completed, or are on the way to finalising industrial projects worth over $430 
million in Oman. Turkish companies are involved in port (Sohar), road 
construction, and water distribution systems. The Blue City Project is the most 
recent success of a Turkish construction company, which, together with a 
partner, is undertaking the responsibility of the completion of the first phase 
of the project.<br>
<br>
And things are really looking up. “The interest of Turkish companies in 
forthcoming Omani projects is growing. The advantage of Turkish companies stems 
from their experience, efficiency, technology and pricing,” claims Turker. 
Pressing the point further, Turker gets into a bit of hardsell: “Turkey, with 
its updated, modernized and investor-friendly legal bases, is an ideal 
destination for Omani businessmen looking to ink investments beyond the borders 
of the Sultanate.”<br>
<b><br>
Advantage Oman</b><br>
“Oman, as the commercial hub between Arabian Peninsula and the sub-continent and 
East Africa, can be a very attractive port for Turkish export products,” 
reiterates the Turkish Ambassador. “Vice-versa, Turkey would also be a perfect 
corner for Omani products to reach the Balkans, Europe, Caucasus and Central 
Asia.”<br>
<br>
No wonder around 200 Turkish business people, representing various sectors, have 
visited Oman and held fruitful talks with their counterparts during the past few 
months. “Under the wise and able leadership of His Majesty Sultan Qaboos bin 
Said, Oman is progressing rapidly,” says Turker. “The developments in Sohar 
clearly demonstrate this. These are all stunning developments. Omani exports are 
bound to shoot up. To add to it, Oman’s flourishing tourism projects are truly 
awesome,” he adds.<br>
<b><br>
Turko-Omani Relations: Historical Background</b><br>
There is more to the Turkey-Oman ties than the current surge in business 
volumes. Historical ties between the two countries date back to hundreds of 
years. “Turks and Omanis were allies. The Ottoman fleet in the 16th century 
unflinchingly shielded Oman against Portuguese attacks aimed at controlling 
trade routes to India. In 1550, Piri Reis, the Commander of the Ottoman Fleet 
assigned to Suez, helped Omani forces recapture Muscat and Al Jalali Fort from 
Portuguese invaders,” elaborates Turker. In the 17th century when Iranians 
forces attacked Basra Port, Oman’s Imam, with an Omani fleet, came to support 
Ottomans and helped repel the Iranian forces. And in the 18th century, when the 
Wahabi’s of Saudi Arabia attacked Ibadis, Ottoman forces came to their rescue.<br>
<b><br>
‘Geographical Allies’ </b><br>
Besides history, geography too fosters brotherly ties between Oman and Turkey, 
feels the Ambassador. “Turkey, with its unique geopolitical and geo-strategic 
location — it’s between Europe and Asia, Balkans’, Caucasus, Middle East, Black 
Sea and Mediterranean — possesses a special importance in regional and world 
politics. Similarly, Oman, with its unique location — at the juncture of Arabian 
peninsula, sub-continent and East African geographical regions — has a unique 
geo-political and geo-strategic eminence.” <br>
<br>
Turker is convinced both Oman and Turkey have a very crucial role to play in 
maintaining international peace and prosperity. “They both have a long, 
traditional, and deep-rooted state experience. And they have both maintained and 
cherished tolerance, and promoted understanding and harmony between different 
cultures, faiths and races.” </font></p>
