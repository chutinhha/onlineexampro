</font><b><font face="Verdana" style="font-size: 14pt">
Losing marginal ground</font></b><font face="Verdana" size="1"><br>
<b>Muscat Securities Market loses some ground as the financial year for most of 
the companies comes to an end </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">T</font><font face="Verdana" size="1">he 
markets made marginal losses during the month of March on the Muscat Securities 
Market (MSM). The benchmark MSM 30 index lost 3.09 per cent to close at 5179.68 
on March 22. All sector indices lost during the month. The Banking and 
Investment Index lost 3.33 per cent, the Industry Index lost 6.65 per cent and 
the Services Index lost 1.29 per cent. </font></p>
<p align="center" dir="ltr"><font face="Verdana" size="1">
<img border="0" src="m_w1.jpg" width="299" height="99"></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Gainers and Losers 
:</b><br>
The top gainers during the period were Flexible Industries (90 per cent), Cement 
and Gypsum (51.90 per cent) and Oman Hol. International (23.81 per cent).</font></p>
<p align="center" dir="ltr"><font face="Verdana" size="1">
<img border="0" src="m_w2.jpg" width="298" height="120"></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
The top losers during the period were Al Ahlia Detergent (55.93 per cent), The 
First Mazoon Fund ( 36.81 per cent) and United Finance (18.70 per cent).</font></p>
<p align="center" dir="ltr"><font face="Verdana" size="1">
<img border="0" src="m_w3.jpg" width="296" height="119"></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">All GCC markets, 
barring Oman, made significant losses during 2006. The maximum loser was Dubai, 
which crashed by 36 per cent during the year. This was followed by Qatar (16 per 
cent), Abu Dhabi (15.6 per cent), Kuwait (9.9 per cent), Saudi Arabia (4.3 per 
cent) and Bahrain (4.1 per cent). Oman markets gained 6.2 per cent this year, up 
to 22 March 2006. It may be noted that due to the recovery in the last few days 
the extent of losses was somewhat reduced in these markets. </font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">In Saudi Arabia, if 
we compare the peak value of 20634 reached by the Tadawul All Share Index on 
25th Feb 2006, there was a substantial fall to 14177 on 15 March 2006, a fall of 
31 per cent in a span of 20 days ! </font></p>
<p align="center" dir="ltr"><font face="Verdana" size="1">
<img border="0" src="m_w4.jpg" width="296" height="194"></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">As we have been 
stressing time and again, MSM is a very attractive market compared to its 
counterparts in the GCC region. MSM is the only market that has weathered the 
storm sweeping across the entire spectrum of GCC stock markets. It is an 
attractive market by any parameter. It is well regulated and controlled compared 
to the other GCC markets. People across the GCC region have taken note of this 
and that’s why it is holding so strong. We strongly advise our readers to stay 
invested in the market in good companies.<br>
Study Findings We have conducted a study on the financials of 125 companies 
listed on MSM and noticed the following interesting points:</font></p>
<ul dir="ltr">
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The aggregate sales 
  of all these 125 companies have grown from RO 1.55 Billion in 2004 to RO 1.85 
  Billion in 2005, registering a growth of 20 per cent.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The aggregate net 
  profit has gone up from RO 281 million in 2004 to RO 347 million in 2005, 
  showing a growth of 24 per cent.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The Net Profit 
  Ratio has gone up from 18.1 per cent in 2004 to 18.7 per cent in 2005.</font></p>
  </li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The profit growth 
  is even steeper in the case of index companies. Out of the 30 index companies, 
  the aggregate net profit has gone up from RO 171 million to RO 226 million, 
  registering a growth of 32 per cent.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The Market P/E for 
  MSM as a whole comes to 13.44. The P/E for MSM 30 index comes to 12.18.</font></p>
  </li>
</ul>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Telecom Sector</b><br>
We have also conducted a study on the financials of the leading telecom 
companies in the GCC sector. Following are the findings:</font></p>
<ul dir="ltr">
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The average P/E for 
  the entire telecom sector in GCC works out to 21.61, compared to the P/E of 
  15.6 for Omantel at the current prices.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The P/E is highest 
  for Saudi Telecom (23.91) and lowest for Bahrain Telecom (10.51).</font></p>
  </li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The Price to Book 
  Value Ratio of Omantel is 4.2 compared to the GCC average of 7.11.</font></p>
  </li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">The Return on Net 
  worth of Omantel is 27 per cent, which is lagging behind the GCC average of 33 
  per cent. However, the GCC is distorted because of the very high RONW of 38 
  per cent for Saudi Telecom. If this extreme figure is removed, Omantel’s 
  average is very much comparable with the GCC average.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">We find that 
  Omantel is quoting at an attractive valuation compared to the GCC average. If 
  the average P/E of 21.61 is applied, Omantel should quote at RO 1.96, which is 
  an appreciation of 38 per cent from the current level.</font></p></li>
</ul>

