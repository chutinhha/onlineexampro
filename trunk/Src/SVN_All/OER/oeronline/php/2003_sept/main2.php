            <font size="1"><b>Even after taking a series of measures to check 
            the problem of loan loss provision, the menace is still haunting 
            major banks. OER reports&nbsp;</b><br>
            <br>
            <img border="0" src="pg42.jpg" align="left" hspace="5" vspace="5" width="250" height="169">While 
            few banks were able to maintain the provisions at the previous 
            year's level, there was a phenomenal growth in provisions of a 
            couple of major banks. This indicates that the issue is not too 
            general, but only concerning to a couple of banks. <br>
            It is not hard to seek the real cause of higher provisions of the 
            banking sector. Few years ago, banks were following an aggressive 
            policy in expanding the loan book and, in the process, compromised 
            on asset quality. &quot;It is a combination of several factors. During 
            the 1998-2000 period, the non-oil sector was not doing well, which 
            affected the banking sector as well. Also, some business groups and 
            individual borrowers faced stock market related issues, which in 
            turn had an adverse effect on the profitability of their companies 
            and their ability to repay loans. However, this problem is sorted 
            out now&quot;, said a senior official at BankMuscat on condition of 
            anonymity. &quot;The Central Bank of Oman had done a good job in sorting 
            out these issues,&quot; the official noted.<br>
            The combined profit of these five banks for the first six-month 
            period fell by almost 16 per cent to RO28.5 million from RO33.5 
            million for the same period last year. Among the major banks, 
            National Bank of Oman (NBO) reported a meagre RO362,000 net profit 
            from RO6 million, while BankMuscat and Oman International Bank (OIB) 
            posted RO13.2 million up from RO12.2 million and RO5.5 million from 
            RO7.2 million, respectively. Bank Dhofar and Oman Arab Bank (OAB) 
            registered RO4.74 million (RO4.10 million) and RO4.70 million 
            (RO4.02 million) net profit respectively. While major banks could 
            not sustain net earnings, conservative financial institutions were 
            able maintain the pace of growth of net profit for the first half 
            this year as compared with the corresponding period the previous 
            year. <br>
            &quot;We have continued to take higher levels of provisions for loan 
            losses at RO12.6 million as compared to the RO8.6 million which was 
            provided for the corresponding period in 2002. The level of 
            provisions in 2003 reflects our now firmly-established policy and 
            best practice of assessing provisions on non-performing loans on a 
            rolling quarterly basis,&quot; said John Finigan, chief executive of NBO. 
            &quot;We have intensified our efforts toward collecting and enhancing the 
            collateral on the non-performing assets (NPAs) portfolio. This has 
            resulted in collections increasing to RO7.9 million for the first 
            half this year as compared to RO2.8 million for the same period in 
            2002,&quot; he added.<br>
            NBO has significantly enhanced collateralisation on a number of 
            classified accounts, Finigan said, adding that the bank has formed a 
            separate remedial management division and a new legal division. With 
            this in place, the level of recoveries is expected to progressively 
            improve.<br>
            The recent stringent provisioning norms by the apex bank calls for 
            provisions against NPAs mandatory to a large extent. But the NPA 
            problem can be brought down only with the banks' ability to 
            understand the financial strength of their corporate clients at an 
            early stage. <br>
            In the last couple of years, CBO has been working overtime to take 
            steps to maintain the health of the banking system. Accordingly, the 
            apex bank has introduced a series of measures, which include 
            stringent norms on related party transactions, asset classification 
            and provisioning, international accounting standards, clear 
            guidelines on corporate governance, disclosure, investment, risk 
            management and asset-liability management. <br>
            The CBO recently collected details of related parties as well as 
            their close relatives for creating a database on their borrowings, 
            which will enable the apex bank to monitor these transactions. 
            According to the present stipulation, a related party (and his 
            relatives) can borrow only up to 10 per cent of the bank's net 
            worth. Further, the collective borrowing of senior management 
            members of any bank should not exceed more than 35 per cent of the 
            bank's net worth. Also, the regulating authorities have introduced 
            stringent guidelines on corporate governance, which is a panacea for 
            many corporate evils. <br>
            Bank managements have not only introduced stringent credit 
            assessment mechanism to bring down NPAs, but have also taken stern 
            action against some of the business groups that owe the banks 
            millions of rials. It may be noted that the net earnings of 
            commercial banks came under severe pressure a couple of years ago 
            due to full provision made for their exposure in a defaulting group. 
            These changes are expected to add more prudence to institutional 
            lending.<br>
            The persistent rise in the NPA levels of a few banks demand further 
            tightening of provisioning. Some banks have not been able to bring 
            down the levels of NPAs to internationally acceptable levels. <br>
            Banks are now focusing on quality lending rather than aggressive 
            lending - a clear change in strategy. &quot;Our strategy to improve the 
            asset quality in the past few years has had a positive impact on the 
            provision for loan impairment, which reduced by 12.9 per cent to 
            RO2.77 million for the first half of this year as against RO3.18 
            million for the same period last year,&quot; stated OIB's chairperson 
            Reem Omar Zawawi. Reem further stated that the bank would continue 
            to focus on recovering non-performing loans and the results continue 
            to be encouraging. &quot;The recoveries for the first half of current 
            year amount to RO1.5 million compared to RO2.7 million for the 
            corresponding period last year,&quot; she added. <br>
            &quot;We expect to see modest growth of our risk asset portfolio in Oman 
            in the remaining moths of the year as we continue to seek high 
            quality lending opportunities on a selective basis,&quot; noted NBO's 
            Finigan. The bank's management has intensified its efforts toward 
            collecting and enhancing the collateral on the NPA portfolio. <br>
            Also, few large banks have already started using Moody's Financial 
            Analyst module to assist in credit assessment, which is expected to 
            add further stringency in the process and thereby bring down NPAs.<br>
            The recent sluggish trend in interest rate was beneficial for the 
            banking sector - deposit rates plummeted while lending rates 
            witnessed a marginal decline. Liquidity in the financial system has 
            improved tremendously in the recent past, which in turn led to a 
            decline in interest rate - especially on deposits. The fall in rate 
            in international markets, especially the US, has influenced the 
            local market. The US Federal Bank interest rate is now at a record 
            40-year low. As the deposit rates have touched a record low, banks 
            are now offering merely 1.25 to 1.5 per cent for term deposits with 
            one-year maturity. However, the average rate of personal loan fell 
            by only 3 to 4 per cent as banks are still charging 10 or 11 per 
            cent for consumer loans. <br>
            In an apparent move to improve interest spread, banks are also 
            switching over to low cost funds by bringing down high cost 
            deposits. &quot;During the first half, low cost deposits demonstrated an 
            increase of 4.3 per cent while high cost deposits declined by 29.9 
            per cent as a result of our conscious policy to improve net interest 
            margin,&quot; added OIB's Reem. <br>
            On the corporate side, although some privatisation projects are 
            coming up, the demand for funds is picking up at a slow pace. Most 
            of the banks are trying to expand their personal loan portfolio, 
            their most lucrative business, besides trying to diversify sources 
            of income by concentrating on fee-based activities. Of late, three 
            banks have started brokerage business. BankMuscat entered the 
            capital market in a big way after acquiring Al Ahlia Securities from 
            ONIC Holding while OAB and OIB commenced broking activity by 
            establishing separate divisions. <br>
            Bankers are optimistic about maintaining the profitability of the 
            institutions as they feel that the efforts taken by the institutions 
            would result in good quality assets in the near future. <br>
            &quot;In general, banks have done well despite high provisions. So, the 
            overall profitability on a consolidated basis should be good for the 
            current year,&quot; said the BankMuscat official.</font></font></p>
