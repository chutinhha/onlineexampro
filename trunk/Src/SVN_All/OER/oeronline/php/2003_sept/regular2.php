            upholstery and fittings are. Again, it's just enough to make you 
            think this good-looking sedan is a cut above some of its rivals in 
            this segment of the market. There are some very thoughtful touches. 
            For instance, there are loads of storage compartments for occupants 
            to stow knick-knacks and there is a button to automatically open the 
            boot which is conveniently located on the driver's door adjacent to 
            his/her power adjustable seat.<br>
            The climate control is worth a special mention. Air from the unit is 
            very efficiently dispensed from ducts - the top-of-the-range model 
            has automatic A/C with an outside temperature display. <br>
            Inside, there's generous space for front and rear occupants. 
            Notably, rear seat passengers have more than adequate headroom and 
            legroom.</font></p>
