            The Epica has quite a calm and composed persona with classic yet 
            simple elegance. It features fine luxury touches usually applied to 
            more expensive cars. A dramatic arching roofline and burly bonnet 
            along with dynamic jewel-like headlamps that are distinctively 
            narrow and sleek. A monochrome appearance and flared fenders give 
            the car a bold, sporty feel. Entry to the Epica is by keyless remote 
            that also has a button to open the boot. The LS model has 15-inch 
            alloy wheels. The LT model has power-folding remote outside mirrors.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Indoors</b><br>
