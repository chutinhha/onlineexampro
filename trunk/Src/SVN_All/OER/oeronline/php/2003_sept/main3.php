            <font face="Verdana" size="1"><b>Two guest managers provide 
            solutions to the hypothetical case of All-New Travel &amp; Tourism by K 
            S Kumar<br>
            </b>
            <br>
            A business strategy identifies the markets and segments that a 
            company chooses to operate in, the set of products/services that the 
            company believes it can offer to its target customers based on 
            perceived competitive differentiators. Note that strategy in smaller 
            companies like All-New Travel &amp; Tourism (ATT) - the fictitious case 
            study outlined in the previous issue of OER - can often be implied 
            and can lead to contradictory actions. <br>
            A strategy has often to be finalised without complete information, 
            and instinct or gut-feel is critical in the process with such 
            qualitative judgements often hidden in the implicit and explicit 
            assumptions that lie behind extensive financials. It goes without 
            saying that errors in strategy can be costly indeed and can sink the 
            mightiest of companies. Given below are two solutions provided by 
            operating managers in Oman.</font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Shankar Bose, GM, Bahwan Travel Agency</b><br>
            <br>
            <b>
            <img border="0" src="pg64.jpg" align="left" hspace="5" vspace="5" width="150" height="171">Market 
            Analysis</b><br>
            While ATT has had two bad years, it is important to remember that 
            all businesses in the travel trade the world over suffered since 
            9/11. There are signs of peace and stability in the region at least 
            now. The government's policy to promote tourism into Oman and the 
            projects currently under way - Sohar Refinery, Oman LNG Third Train, 
            Omifco Fertiliser, Bahwan Fertilisers, Sohar Port etc. - would 
            result in employment and generate air traffic for workers and 
            businessmen and also help in the process of Omanisation. Salalah 
            Port with a FTZ could become a Pan Arab distribution point because 
            of the geographical advantage of lying on the main East/West sea 
            route.<br>
            The Tourism Department is appointing market representatives all over 
            Europe, Russia, Australia and India, and with the easing of visa 
            rules, there would be a flood of visitors in the years ahead. The 
            hotels are working on several group quotations referred to them by 
            the big inbound players and newspapers have reported the possibility 
            of a FIA-certified off-road rally in Oman in 2004. The Khareef 
            Festival in Salalah was great this year with all hotels completely 
            sold out. Interest in Oman is growing at a rapid pace. <br>
            <br>
            <b>Suggestions</b><br>
            The current difficulties should not deter Ali, the owner of ATT, 
            from sticking with his view that they were in a business with good 
            potential. They, however, need to commit themselves fully without 
            reservation as only then can they exploit all possible 
            opportunities. ATT should exploit the inbound tourism potential and 
            the proposed GCC tie-up is a must.<br>
            I do not feel the necessity of appointing reps in GCC countries 
            because operators in this region know the product and can position 
            it appropriately. And sales calls can be made to promote specific 
            seasons/products/events at a marginal cost. What is needed is to 
            appoint reps in main markets of Europe/Russia/Scandinavia, where the 
            knowledge of Oman is not too good. Cooperation with Dubai-based 
            operators is necessary to take advantage of the great promos that 
            Department of Tourism and Commerce Marketing does. <br>
            Ali should not look at the company's business in a segmented way. He 
            should take an integrated approach because every segment complements 
            the other and would transform ATT from a travel agency into an 
            enterprise to partner their clients in growing businesses. This 
            would also help as the inbound business is seasonal and lasts for 
            about seven months a year.<br>
            Therefore, not only should ATT develop the ticketing/reservation, 
            inbound tours and outbound holidays functions, it should now 
            consider acquiring an international car rental franchise, commence 
            development activities to represent airlines in Oman as GSAs, 
            dispense Travellers Cheques and Holiday Insurance services, offer 
            meet-and-greet facilities and gradually expand the branch network so 
            as to be in close proximity of their valued customers. <br>
            Importantly, ATT should import current technology so that its 
            business processes could go real-time, making transactions faster 
            and cost-efficient. At the core, of course, there has to be an 
            ongoing Training and Development Program. <br>
            The Oman ticketing business is estimated at RO75 million per annum. 
            ATT should focus on all sectors of this market. Therefore, ATT can 
            have dedicated staff for ministries, corporates, high net worth 
            individuals, group companies, agency networks etc. A branch at 
            Rusayl may become essential given the growing development in that 
            area. This way, ATT could increase its market share.<br>
            Outbound holidays can also be strengthened. Consider the huge exodus 
            to the Far East this year in spite of the SARS scare. Planes to 
            Bangkok, Kuala Lumpur and Australia were going full to capacity. 
            People may not have been travelling to the States, but the European 
            Coach Tours, Disneyland Paris and the Swiss Packages sold like hot 
            cakes. ATT should not wind down this lucrative part of their 
            business. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>KM Sreenivas, GM, Nabil Biscuits</b><br>
            <br>
            <b>
            <img border="0" src="pg65.jpg" align="right" hspace="5" vspace="5" width="150" height="174">Why 
            ATT should continue </b><br>
            Tourism is a promising industry and is expected to grow 
            significantly in the coming years. Although it can be debated as to 
            how realistic the projections are in terms of numbers, the current 
            time is not for withdrawal. With the exception of the last five 
            years, the company has been profitable.<br>
            The investment in the company is sizeable and every effort should be 
            made to turn the business around, if feasible. ATT has some in-house 
            travel business and along with a clear business strategy for 
            revival, cost-reduction measures can be carried out to improve 
            operational efficiencies. Although there is serious erosion of net 
            worth, it is still positive and the company is not under any 
            bankruptcy threat. <br>
            <br>
            <b>What needs to be done</b><br>
            The problem with ATT appears to be that it failed to chalk out a 
            clear strategy for its business. Ali should follow his instinct 
            while keeping in mind the risks highlighted by the numbers. Broadly, 
            the market can be segmented into three areas:</font></p>
            <ul>
              <li><font face="Verdana" size="1">Domestic tourism</font></li>
              <li><font face="Verdana" size="1">Inbound tourism (GCC based; Non-GCC 
              based) </font></li>
              <li><font face="Verdana" size="1">Outbound tourism (Europe bound; 
              US/Canada; African destinations; Far East)</font></li>
            </ul>
            <p><font face="Verdana" size="1">It can further be segmented based 
            on the type of tourism such as:</font></p>
            <ul>
              <li><font face="Verdana" size="1">Eco-tourism</font></li>
              <li><font face="Verdana" size="1">Adventure tourism</font></li>
            </ul>
            <p align="justify"><font face="Verdana" size="1">It can be even further be segmented 
            by income. ATT does not seem to have strengths to cater to the low 
            margin, outbound tourism, where the servicing and knowledge 
            requirements are considerable. The fact that it is not a GSA for any 
            major airline reduces the possibility of volumes. It should limit 
            its business in this area only to its group companies unless it can 
            become GSA for any major airline.<br>
            Amongst the domestic and inbound sectors, there are 24 distinct 
            segments. The package of services to be offered, the pricing, the 
            promotion, the resource requirement and the training needs of the 
            staff are different for each of these segments. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Defining the target customers</b><br>
            In terms of inbound tourists, clear identification of the segment to 
            which the company would like to cater, keeping in mind its strengths 
            is a must. For a smaller player like ATT, it is important to develop 
            niche products to a niche segment and do a good job of catering to 
            that niche. For example, one niche could be eco-tourism catering to 
            middle income tourists. Large companies, given their overheads, will 
            find it difficult to address this. Besides, there is always an 
            advantage of the pioneer.<br>
            Smaller players, in the current scenario definitely need to enter 
            into tie-ups with global players. In such a case, it will combine 
            the advantages of local knowledge and low overheads of an Omani 
            company and the global reach of the overseas company. <br>
            The company, surprisingly, also seems to have ignored the domestic 
            tourism market. People who are already residing in the country are a 
            definite target market and products can be tailor-made to address 
            this segment.<br>
            Product development and promotion will follow once the strategy is 
            laid out and will lead to identification of training and resource 
            needs and mobilisation and so on. After the detailed work out, the 
            strategy needs to be implemented, with proper milestones for 
            measurement of results against the strategic objectives. <br>
            <br>
            <b>Conclusion<br>
            </b>While the ultimate decision lies with Ali, in case he decides to 
            continue with ATT, he should:</font></p>
            <ul>
              <li><font face="Verdana" size="1">Focus on niches like eco-tourism 
              to maintain a faster return on investment.</font></li>
              <li><font face="Verdana" size="1">Form strategic alliances with 
              industry players to share the risks.</font></li>
              <li><font face="Verdana" size="1">Consider partnering with an 
              airline as its GSA</font></li>
              <li><font face="Verdana" size="1">Follow an ambitious rescue plan 
              by broadening its search for more revenue streams.</font></li>
            </ul>
