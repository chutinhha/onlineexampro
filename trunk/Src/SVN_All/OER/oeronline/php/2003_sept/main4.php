            <font face="Verdana" size="1"><b>From Lagos to Lusaka, from Accra to 
            Antananarivo, a wave of excitement and optimism is emanating from 
            Sub-Saharan. Despite innumerable false dawns in the continent over 
            the past half century, many economists are confidently predicting a 
