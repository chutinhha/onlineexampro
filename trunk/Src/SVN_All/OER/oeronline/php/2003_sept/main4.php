            <font face="Verdana" size="1">An African Renaissance represents not 
            only a chance for the world's poorest people to dramatically improve 
            their living standards, but it also offers tremendous opportunities 
            for those countries that recognise the trade and investment 
            potential of Africa. Oman is in a strong position to take advantage 
            of this opportunity, but it will need a concerted effort by 
            government and industry, and well-planned and active programme to 
            identify and take advantage of opportunities to increase trade with 
            Sub-Saharan Africa.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>A new dawn in Africa</b><br>
            With active civil wars raging throughout the continent, AIDS 
            ravaging the most productive sections of the working population in 
            many countries and kleptocrats continuing to grab headlines, perhaps 
            this seems an odd time to be touting the potential of Africa. 
            Indeed, the social and economic fortunes of the continent have 
            continued to decline steadily over the past 20 years - in fact, 
            Africa's share of world exports fell by more than 25 per cent 
            between 1990 and 2000.<br>
            The road ahead will not be easy. Simply reversing the downward trend 
            will take some doing. But there is reason to be sanguine. And this 
            is primarily because the people and the leaders of Africa are, for 
            the first time, taking economic development in their own hands.<br>
            Reforms are sweeping Africa. Democratically elected heads of state 
            now govern in more African countries than ever before. The New 
            Partnership for Africa's Development (NEPAD) is the platform for one 
            of the most important African initiatives in the past half century. 
            NEPAD is an African-initiated, African-run programme that aims to 
            ensure the sustainable development of the continent - economically, 
            politically and socially - by focusing on trade and indigenous 
            investment rather than aid and (often crippling). Through a 
            programme of economic development underpinned by good governance, 
            NEPAD aims to bring Africa to the world as a viable and equal 
            business partner in the global market.<br>
            Economic reforms and trade development are also making headway 
            throughout the continent. Tariffs are on the decline and trade on 
            the upswing. Various initiatives have helped to shift the mindset of 
            African industry and gear them up for trade. Significant 
            infrastructure investment is supporting the development of trade 
            logistics - critical for a peripheral continent with so many 
            landlocked states. Projects like the Dube Tradeport, which aims to 
            create a multi-modal logistics gateway to Africa through Durban, 
            show the vision and potential for massive trade growth on the 
            continent.<br>
            Africa offers much to the prospective trader, including a huge 
            market of 700 million people, a rich supply of natural resources, 
            and a low-cost production base. For Oman, which has a natural 
            historical and geographical relationship with Africa, it represents 
            a huge, untapped area of potential.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Oman - Africa trade: low and 
            imbalanced</b><br>
            Africa's trade patterns remain inextricably linked with their 
            colonial past - more than 50 per cent of Africa's current trade is 
            with Europe. The Middle East by contrast contributes a mere 4 per 
            cent of Africa's imports, and this even includes oil imports. 
            Looking at non-oil trade, the GCC region overall plays an 
            insignificant role in African trade. In fact, virtually all non-oil 
            export to Africa from the GCC represents re-exports of goods coming 
            from Asia and Europe. And almost all of this is coming from Dubai, 
            which is one of the only Gulf States that has developed wide trade 
            links with Africa.<br>
            Oman's trade relationship with Africa remains extremely limited, and 
            highly imbalanced, with oil and gas exports dominating. In 2000, 
            Africa accounted for only 2 per cent of Oman's non-oil trade. On the 
            import side, the numbers are most shocking - only 0.3 per cent of 
            Oman's imports come from non-Arab African countries. Exports from 
            Oman to Africa are higher, but like Dubai, the majority of these 
            exports are in fact re-exports (and thus not Omani-made products).<br>
            What is also striking about Oman's trade relationship with Africa is 
            just how imbalanced it is. Seventy-five per cent of Oman's exports 
            to Africa go to Tanzania, Kenya and Zambia, and virtually all of 
            this is oil and gas. In contrast, South Africa and Zimbabwe alone 
            account for 75 per cent of Africa's exports to Oman. Looking at the 
            top 10 African economies, Oman has relatively balanced trade with 
            only one: Mauritius. With South Africa and Zimbabwe, Oman has a 
            large trade deficit, and with all others Oman's oil exports have 
            created huge trade surpluses.<br>
            Balanced trading relationships open up more transport links, improve 
            business ties and develop increased opportunities for links across 
            supply chains. They also help support the development of more 
            specialised economic structures based on sources of comparative 
            advantage. Improving bilateral trade with Africa should therefore be 
            a major focus area for Oman.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Opportunities for bilateral 
            trade</b><br>
            Analysis of sectoral patterns of trade suggest significant 
            opportunities to expand bilateral trade between Oman and Africa.<br>
            For Omani exporters, Africa offers a potentially lucrative market 
            for Omani-made products in such areas as prepared foods, textiles 
            and clothing, electronics, pharmaceuticals and chemicals. In 
            addition, Oman has the potential to significantly increase its share 
            of the re-export market to Africa. With the African market growing, 
            and Oman's geographical position, African can represent a major 
            platform by which Oman can increase its industrial base and support 
            diversification.<br>
            For Omani importers, Africa represents a potentially competitive 
            source for such products as agricultural commodities, minerals and 
            metals, wood products, cotton and textiles and certain chemicals and 
            fertilisers. In addition, there are likely to be a wide range of 
            other niche products that can be competitively sourced from Africa. 
            Oman sources many of these products from Europe and Asia - in fact 
            much of it comes by way of the Dubai re-export market (and so, some 
            of it may in fact be of African origin). For Oman, finding more 
            cost-effective sources of key imports will significantly improve its 
            own industrial competitiveness, and thus will have a knock-on effect 
            on Oman's export potential. Moreover, sourcing commodities from 
            Africa provides Oman with an opportunity to develop value-added 
            processing for further export into the Gulf region.<br>
            What is missing that prevents Oman from fulfilling its trade 
            potential with Africa? Historically civil strife, macroeconomic 
            stability and protectionism dampened trade potential. The barriers 
            are coming down on all of these. Lack of transportation links has 
            traditionally been another limitation to trade. However, the 
            development of the port and free zone at Salalah provides Oman with 
            an opportunity to significantly improve its transport links with 
            Africa. <br>
            But the biggest barriers to trade are simply lack of information and 
            communication. Omani industry needs to be more aware of trade and 
            investment opportunities with Africa, and African industry of the 
            opportunities with Oman. New opportunities need to be identified and 
            developed. It is the responsibility of both Government and Industry 
            to put in place and active programme to develop these trade 
            opportunities.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Developing trade opportunities 
            with Africa</b><br>
            The main issue is to develop and enhance the institutional links 
            between Oman's Government and Industry and their counterparts 
            throughout Africa. Whilst such institutions may already be there in 
            parts, to effectively exploit these opportunities, a full programme 
            should be developed and implemented explicitly to support trade with 
            Africa.<br>
            The first step is to involve all the key Omani stakeholders. This 
            should include the Ministry of Commerce and Industry (MCI), 
            sector-based business groups or industry associations, key industry 
            leaders, and commercial offices within Oman's embassies based in 
            Africa. In addition, Oman's trade promotion organisations like 
            OCIPED and Oman Chamber of Commerce and Industry should play the 
            leading role in such a project.<br>
            The next step is to identify the trade opportunities on a 
            sector-by-sector and market-by-market basis. In-depth market 
            assessment should be conducted to identify the main opportunities 
            for bilateral trade. Such market assessment should not only be a 
            one-off exercise, but should be institutionalised within MCI and/or 
            sector-based institutions.<br>
            In addition to these activities, market development support should 
            be provided to Omani exporters looking to enter African markets. 
            Such market development support could include initiatives like 
            marketing and extending the &quot;Omani-Made Products&quot; programme and 
            providing incentives and subsidies on transport, warehousing, 
            financing or marketing to target African markets. <br>
            Finally, the Government can support trade with African by further 
            improving transport links, thus reducing logistics costs and 
            increasing the opportunities for interaction between Oman and 
            Africa. Recent investments in Salalah Ports and in Seeb 
            International Airport can provide a platform for trade growth. 
            Developing strategic links between these facilities and African hubs 
            like Ghana, Kenya and South Africa. <br>
            The African Renaissance is coming. With effective planning and 
            implementation, Oman can reap enormous benefits from expanding its 
            trading links with this vibrant continent.<br>
            <br>
