                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="pg62.jpg" align="left" hspace="5" vspace="5" width="300" height="215">He 
                    sure is a tough cookie. As a young boy of 17, Salim Tamaan 
                    Musallam Al Mashani survived without food or water for three 
                    days in the deserts of the Al Mahra region in Yemen when he 
                    got lost while on a hunting spree with his friends. <br>
                    He survived on wild grass, walked barefoot after his shoes 
                    wore off and maintained a daily log of events in a diary 
                    besides mentioning in it that he had got lost on his own and 
                    that it wasn't his friends who were responsible for it to 
                    avoid trouble within his tribe in case he couldn't find his 
                    way out and succumbed in the desert itself.<br>
                    &quot;For the first two days, I had no clue about where I was and 
                    what I were to do. On the third, however, I got my bearings 
                    together and, looking at the stars for navigation, I started 
                    figuring my way around,&quot; he reminisces. &quot;I was so exhausted 
                    that I set targets - 100 metres at a time - and moved on 
                    from 4 'o clock in the evening till I reached habitation at 
                    around 10 in the night,&quot; he adds.<br>
                    That small anecdote makes known three things about the man 
                    who today is in charge of affairs at Dhofar Fisheries 
                    Industries Company SAOG (DFICO). A never-say-die attitude 
                    that persuaded him not to give up in the face of adversity 
                    and survive on whatever was available in order to go on and 
                    achieve his objective; a profound foresight that made him 
                    see that his friends and their families could be in trouble 
                    in case someone from his family believed that he was killed 
                    by them and dumped in the desert, thus writing an account in 
                    his diary so as to vindicate them; and his ability to break 
                    down bigger, seemingly unattainable goals, into smaller, 
                    more manageable targets. <br>
                    <br>
                    <b>In troubled waters</b><br>
                    A fourth observation could be that he eventually finds his 
                    way around. That, in a nutshell, is Mashani, the man who, as 
                    its general manager, has been entrusted with the 
                    responsibility of turning around DFICO, a company that has 
                    definitely seen better days. The current market 
                    capitalisation of DFICO, at RO1.950 million (August 2003), 
                    is a shadow of the RO5 million that it once used to be. <br>
                    Moreover, DFICO achieved a turnover of RO301,643 during the 
                    half-year ended June 30, 2003, incurring a loss of 
                    RO1,113,432 for the period. The cumulative carried forward 
                    loss as on June 30, 2003, amounted to a whopping RO5 
                    million. So how is he going to navigate the company's 
                    balance sheet from red into black? &quot;By breaking it up into 
                    achievable targets, of course,&quot; pat comes the reply. &quot;The 
                    journey of a thousand miles begins with a single step,&quot; 
                    quotes Mashani. &quot;Planning is of utmost importance for 
                    success, and that is exactly what we are doing right now,&quot; 
                    he points out.<br>
                    Never one to shy away from calling a spade a spade, Mashani 
                    acknowledges the problems that DFICO is going through, but 
                    reaffirms management's commitment to turn the company 
                    around. &quot;We are going through the worst phase of the company 
                    at the moment, but we are working hard to ensure that that 
                    changes sooner rather than later. We have now leased a small 
                    vessel which we are making ready for different types of 
                    catch. Moreover, we are working on cash basis with some of 
                    our key clients, and hope to be in some shape by the end of 
                    this year. By end-2004, we hope to return to profitability,&quot; 
                    declares Mashani.<br>
                    <br>
                    <b>Casting the net</b><br>
                    After completing his Business Administration from Western 
                    International University in the UK, Mashani began his career 
                    with Oman Mining, where he worked for five years as 
                    marketing coordinator. There on, he joined the Muscat 
                    Overseas Group, where he initially worked as Administration 
                    Manager for Muscat Overseas Oil and Gas Drilling Co. LLC. In 
                    1996, he was promoted to Director of the Vice Chairman's 
                    office, in the same group, looking after the various 
                    investments projects of the group, a position he held till 
                    1999, when he moved on to DFICO.<br>
                    On his way to becoming the GM of DFICO, Mashani has been on 
                    the board of directors of various organisations, including 
                    Oman International Bank, Oman Air, Dhofar Fisheries, 
                    National Rice Mills, BankMuscat and Dhofar Insurance, with 
                    him still serving the boards of the last two organisations.<br>
                    From mining to oilfields to fisheries, it sure must be a 
                    long haul. &quot;It certainly gave me a wide knowledge base, with 
                    a different kind of experience in each one of the industries 
                    I served, which gave me a lot of opportunity to develop my 
                    management skills,&quot; maintains Mashani. &quot;While technically 
                    there was a difference in each of the companies I worked for 
                    in respect of the goods that they produced and also in the 
                    marketing styles of those products, the underlying 
                    management philosophy is the same - you have to maintain the 
                    company's profitability,&quot; he insists.<br>
                    Making a place for DFICO in the Sultanate's fishing 
                    industry, &quot;which until now was unorganised and 
                    underdeveloped,&quot; is what Mashani considers to be his biggest 
                    achievement. Oman currently stands fourth in the Arab world 
                    in terms of catch. The performance of the industry owes in 
                    part to its composition - about 90 per cent of the total 
                    catch comes from the traditional fishermen who operate near 
                    the coast. Only 10 per cent comes from the 24 Omani 
                    fisheries companies licensed to fish in deeper waters. <br>
                    <br>
                    <b>Shoring up</b><br>
                    A self-confessed delegator, Mashani believes in the ability 
                    of his staff members and is a team player to the core. &quot;DFICO 
                    is not a one-man show,&quot; he insists. &quot;It is teamwork that 
                    will see us through. My job is to put systems in place, and 
                    it is the job of the respective departments to implement the 
                    organisation's strategy,&quot; he adds. Of course, while 
                    implementation is, indeed left to the various departments, 
                    most of Mashani's time is spent in monitoring the company's 
                    developments at regular intervals so as to ensure that it 
                    stays put on the road to recovery. <br>
                    For the record, around 95 per cent of DFICO's produce is 
                    exported to, besides the Gulf States, Libya, South Africa, 
                    the UK, Portuguese and Spain. &quot;Turning around DFICO is now 
                    the biggest challenge that I face and, hopefully, we should 
                    be able to manage that.&quot; An indicator of that is the fact 
                    that DFICO has been able to secure orders for twice the 
                    company's current capacity, and is working hard to up the 
                    ante by undergoing internal restructuring as well as looking 
                    for ways to enhance its capability to provide raw material 
                    for the factory. <br>
                    DFICO has invested RO15 million in its cold storage and 
                    brine systems. Most processing companies depend on import of 
                    raw material, which raises the price of the final product. 
                    &quot;If the raw material were available, we could produce twice 
                    as much as we do now,&quot; claims Mashani. Currently in its 
                    second year of operations, DFIC's capacity utilisation is 
                    just at 35 per cent, importing about 90 per cent of its raw 
                    material, mostly from Yemen and India. <br>
                    <br>
                    <b>Charting the course</b><br>
                    Mashani's typical day begins at 6 am when, after prayers, he 
                    likes to indulge in reading. After food for thought, it's 
                    time to spend some time with his family on the breakfast 
                    table and then he's off to work, which begins at 8 and goes 
                    on till 3 in the afternoon. &quot;I generally leave office by 4-5 
                    pm, after which I want to spend some quality time with my 
                    family,&quot; avers Mashani. <br>
                    His family comprises of his wife and four school-going 
                    children (three boys and a girl), with his 16-year-old 
                    eldest son at the verge of finishing schooling. So what does 
                    he want his children to become? &quot;The role of the family is 
                    one of extending support to its members, not to dictate 
                    terms,&quot; he says. &quot;I will stand by my children and guide them 
                    in whatever vocation they choose,&quot; adds Mashani.<br>
                    His favourite hobby? No prizes for guessing that right - 
                    yes, it's fishing. &quot;Fishing has been a passion with me ever 
                    since my childhood, when I started off by catching fish in a 
                    small pond in Yemen. Even today, I go out to the sea to 
                    catch fish whenever I can find the time to do so. It's a 
                    pretty useful hobby.&quot; A hobby which this entrepreneur has 
                    certainly put to good use.</font></p>
