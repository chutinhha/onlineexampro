            your manners!</font><p align="justify"><font face="Verdana" size="1">
            <b>here's more to corporate etiquette than just using the right 
            forks and knives. Nowfaleena Ebrahim talks about the devil in the 
            details</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>H</b></font><font face="Verdana" size="1">ave you ever been in a 
            situation where you didn't know how to use your cutlery, or worse 
            still walked into a corporate or social luncheon feeling 
            uncomfortable and felt your self-confidence ebb away? Well then, 
            instances like these and others could actually cost you a deal. &quot;The 
            way we talk, dress and behave can have an influence over people. 
            Etiquette is not only about good behaviour that influences people in 
            a positive manner, but also about putting the other person at ease 
            and creating winning situations for all,&quot; says Jasmin Dibsi, founder 
            and director of The Etiquette House in Abu Dhabi. A well-known 
            personality in the UAE, she's soon going to spread her wings in the 
            Sultanate. <br>
            <br>
            After graduating from the American College of Switzerland, Jasmin 
            continued her studies at the Florida Atlantic University, where she 
            majored in International Business. She then went on to get trained 
            and certified as a Corporate Etiquette and International Protocol 
            Consultant by the Protocol School of Washington, USA. Her education 
            and multicultural background have given her a solid base for 
            operating in culturally diverse environments. <br>
            <br>
            The programme, or rather the concept, has brought a sigh of relief 
            to the people in the corporate world. But what is it that made 
            Jasmin step into Oman to train the people here? Well, the whole 
            launch of The Etiquette House in the Sultanate is the vision of 
            Amjad Siddique, director, Al Zurikha Trading LLC. The seminars and 
            programmes in Muscat will be held in association with Al Zurikha, 
            the representative of The Etiquette House in Oman. A partner and a 
            mediator both, Amjad himself is very particular about the way he 
            conducts himself. And it was he who brought Jasmin into the 
            Sultanate. &quot;Business etiquette aside, mannerisms begin at home,&quot; 
            admits Amjad, who thanks his parents for having groomed him well. 
            &quot;As much as it applies to the corporates, it also applies to 
            children and housewives, who can benefit a great deal.&quot; <br>
            <br>
            How and why did Amjad think of bringing the concept to Oman? 
            &quot;Because we have nothing like this in the Sultanate. In my 13 years 
            of existence and experience in Oman, I can proudly say that this 
            concept is new and will be much in demand once the training starts,&quot; 
            he avers. Agreeing with him on this note is P.V. Venkatesh, general 
            manager (Human Resource Department), Khimji's Group. &quot;Our group has, 
            during the training analysis, felt the need for a programme like 
            this many a times. But there hasn't been anything as promising as 
            this one&quot; says Venkatesh, who had recently attended Jasmin's 
            etiquette training session, which he found very impressive. He's 
            planning to get his group enrolled in the upcoming session and 
            expecting productive results. <br>
            <br>
            Although business etiquette is made up of significantly more 
            important things than<img border="0" src="amjad.jpg" align="left" hspace="5" vspace="2" width="150" height="202"> 
            knowing which fork to use at the third course during a lunch with a 
            client, it is important to take notice of the fact that, 
            unfortunately, in the perception of others, the devil is in the 
            details! &quot;Acquiring etiquette and protocol skills distinguishes a 
            person from another in the way he or she handles himself or herself 
            in different situations. It projects a professional image, along 
            with confidence and authority. A person becomes more at ease in the 
            business and the social arena. Only once a person knows what to do 
            and how to do it would I consider my training successful,&quot; declares 
            Jasmin. <br>
            <br>
            While you may feel that etiquette is about presenting yourself with 
            the kind of polish that shows you can be taken seriously, where and 
            how does one draw the line? Isn't etiquette also about being 
            comfortable around people and making them comfortable around you? &quot;I 
            would say 60 per cent of the attendees do change 180-degrees because 
            they do want to change and they do practice the skills they acquired 
            so that these skills become part of their daily routine. A good 25 
            per cent would change 90-degrees because they cannot completely 
            unlearn their habits; so, they combine both. And 10 per cent need 
            constant reminders and 5 per cent would think: &quot;I am comfortable 
            with the way I am', they do however apply certain skills to their 
            daily routine especially what is related to handshaking, eye 
            contact, business cards etc.,&quot; justifies Jasmin. <br>
            <br>
            There are many written and unwritten rules and guidelines for 
            etiquette, and it certainly behooves a businessperson to learn them. 
            The hunch is that there is no possible way to know all of them! 'To 
            many people, etiquette meant aristocratic codes of behaviour or how 
            the cr-me de la creme behaved; which meant white gloves, being a 
            snob, and so on. It did take some time to educate people about the 
            meaning of etiquette especially in relation to children. Now, 
            however, I have clients from different social and income status, 
            age, genders. It's a mix. So, I guess people are becoming more aware 
            of the need for such skills,' says Jasmin. <br>
            <br>
            &quot;Our business managers and team members interact with people of 
            various nationalities and hence this programme, I hope, will bring 
            in some kind of consistency in the way international clients are 
            handled,' adds Venkatesh of Khimji's. While talking about 
            international patrons, one does realise that etiquette differs from 
            place to place. Like the mannerisms in the West may not necessarily 
            apply to those in the Middle East. So, how does a training session 
            tackle these issues, especially since the target is a corporate 
            client (who understandably does a fair amount of travelling)? <br>
            <br>
            &quot;The basics and cornerstones of etiquette are the same especially 
            when we are talking about corporate etiquette. The difference lies 
            in the cultures and customs. Part of the training, especially when 
            we cover the international business etiquette and protocol for the 
            client, goes more in-depth into body language, gestures, and 
            traditions, cultures of countries that these clients do visit or are 
            planning to visit. We discuss monochronic and polychronic cultures, 
            high and low context messages, perceptions and misperceptions, gift 
            giving protocol, and so on. And this is where the difference does 
            exist, from culture to culture,' replies Jasmin.<br>
            <br>
            Amjad believes that Jasmin's upbringing is of considerable 
            importance when it comes to etiquette in this region. &quot;She hails 
            from a Syrian-German background, which means she has a good mix of 
            the East as well as the West. Also, having lived most of her life in 
            the Arab region, it gives her the upper hand in mastering the art of 
            etiquette in this region,' says he. It's interesting to note that, 
            besides the corporate houses, Jasmin also trains housewives and 
            children. She is also certified as a Children's Etiquette Consultant 
            by the Etiquette Connection in the USA, and has conducted youth 
            etiquette courses for different age groups ranging from four to 20 
            years regularly across the UAE. What is noteworthy is that she has 
            specific courses designed for specific target groups, all based on 
            professional research. <br>
            <br>
            On a general note, there are many who feel that an etiquette class 
            is the last thing he/ she wants to admit he/ she has been to! But 
            Jasmin believes that it's time people came  <img border="0" src="jasmin.jpg" align="right" hspace="5" vspace="2" width="150" height="202">forward. She says: 
            &quot;Everyone is looking for an edge in today's business 
            world, and so it is important to educate people and corporate 
            decision makers about the new meaning of etiquette and protocol. It 
            is all about proper behaviour and how acquiring these skills plays a 
            big part in putting themselves and the company they are working for 
            in the frontline, because it improves dealing with people and boosts 
            confidence, which ultimately results in better service and projects 
            professionalism.' She adds: 'Employees, especially the ones dealing 
            directly with customers, are considered to be company ambassadors, 
            and if they handle themselves in a professional manner along with 
            the products they are providing, the customers and clients will 
            definitely go back to them instead of going to another company that 
            has the exact same knowledge or product but less efficient people 
            skills management.&quot;<br>
            <br>
            In the words of George Bernard Shaw: 'There is no accomplishment so 
            easy to acquire as politeness, and none so profitable,' yet 
            unintentional breach of manners and faux pas could ruin one's day, 
            or worse, one's deal, but it's a call taken by the corporate houses 
            in the Sultanate to improvise and hone these skills. What remains to 
            be seen is to what heights these etiquette classes take them! </font></p>

