                    <b>TRA introduces 8-digit phone numbers</b><br>
                    <br>
                    The Telecommunications Regulatory Authority (TRA) has 
                    announced the implementation of a new numbering plan for 
                    various public telecom services provided at present by 
                    Omantel, namely fixed lines, GSM and prepaid services. The 
                    implementation of the new plan will result in changing the 
                    numbers used by the subscribers. <br>
                    <br>
                    The numbering plan is a national resource with finite 
                    capacity. With the potential for the future introduction of 
                    competition into the sector, it is a requirement that all 
                    future operators have equivalent access to the numbering 
                    resources and that they can receive allocations from within 
                    the appropriate plan. <br>
                    <br>
                    The existing national numbering plan is critically short of 
                    capacity and requires reform. The changes adopted within the 
                    plan to implement eight-digit numbering scheme has been 
                    designed to create both additional capacity and to ensure, 
                    as far as possible, flexibility to allow unhindered 
                    development of the national numbering plan in the future.
                    <br>
                    <br>
                    Therefore, TRA responsibilities are to ensure that adequate 
                    numbers are available for all telecommunications services 
                    throughout the Sultanate. The existing numbering plan is a 
                    six-digit (exceptionally, seven digits) scheme. It is 
                    running short of capacity and suffering from a range of 
                    problems such as: <br>
