                    <b>OPP awards $240m financing mandate<br>
                    </b><br>
                    The 80 per cent Oman Oil Company-owned Oman Polypropylene (OPP) 
                    LLC has mandated a group of four banks to arrange $240 
                    million (RO92.31 million) of financing facilities to part 
                    finance the construction of its new polypropylene plant. The 
                    lending institutions - Arab Banking Corporation, Arab 
                    Petroleum Investment Corporation, BNP Paribas and HSBC Bank 
                    Plc - have underwritten the full amount of facilities and 
                    will syndicate the transaction shortly after signing the 
                    facility agreement. <br>
                    <br>
                    LG International holds 20 per cent stake in the capital of 
                    OPP. The project will have an initial capacity to produce 
                    340,000 tonnes per annum of various grades of polypropylene. 
                    Propylene feedstock will be supplied by Sohar Refinery 
                    Company, which is currently constructing a new 116,000 
                    barrels per stream-day deep conversion refinery on an 
                    adjacent site at the Sohar industrial area. A consortium of 
                    LGI and LG Engineering and Construction Corporation will 
                    construct the plant. LGI will also act as marketer and 
                    off-taker for the majority of polypropylene produced by the 
                    plant. <br>
