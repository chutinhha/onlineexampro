            <p align="justify"><font face="Verdana" size="1">
            <b>Kuwait leads the region with the most competitive GSM rates. No 
            surprise this, given that it was the first GCC country to open its 
            mobile telephony market to competition five years ago<br>
            <br><center>
            <img border="0" src="table_kuwait.jpg" width="394" height="188"></center>
            <p align="justify">
            </b>There are 1.7 million mobile subscribers in Kuwait, translating into 
            a penetration rate of 73 per cent. The high penetration is the 
            healthy result of the duopoly competition between Kuwait's two 
            mobile operators, MTC-Vodafone and Wataniya Telecom. As the 
            competition for market dominance in Kuwait continues, and with the 
            cellular market nearing saturation, the operators have been 
            scrambling to achieve the technological edge to win over customers. 
            Kuwaiti mobile users already enjoy services such as online bill 
            payment, GPRS services that offer downloads of music and games, and 
            MMS services. <br>
            <br>
            The two GSM operators&nbsp; MTC-Vodafone (recently bagged its one 
            millionth customer) and Wataniya&nbsp; are neck to neck in market 
            share, with MTC maintaining its edge in post-paid market from last 
            year, and Wataniya pulling way ahead in the pre-paid sector. With 
            the Kuwaiti market approaching saturation, the two operators have 
            expanded their operations outside Kuwait, establishing themselves as 
            strong regional operators, bagging a chunk of the recent licences 
            awarded. MTC reversed the 2002 results in 2003 and grabbed 57 per 
            cent of total market additions in 2003. This has helped it inch up 
            its market share from 53 per cent in 2002 to 54 per cent in 2003.
            <br>
            <br>
            Telecommunications services in Kuwait are partly liberalised and 
            privatised. The government acts as both regulator and monopoly 
            provider of fixed-line services but there is competition in data 
            service provision and Internet provision and a duopoly in mobile 
            services. Mobile penetration is high, second only to the UAE in the 
            region. The government has significant shares in both mobile 
            operators. Both mobile operators are expanding, acquiring existing 
            operators and new licences in the region. Both operators are part of 
            consortiums to have won licences in Iraq. <br>
            <br>
            According to AAG research analyst Yaman Al Jundi, &quot;Kuwait remains a 
            predominantly prepaid market, with prepaid subscribers constituting 
            almost 80 per cent of cellular users. Wataniya holds the edge in the 
            prepaid market, with a 53 per cent market share. MTC-Vodafone holds 
            a dominant 81 per cent of the post-paid market share. The mobile 
            market grew by 30 per cent in 2003. MTC-Vodafone lost post-paid 
            subscribers for the third straight year, recording a 4 per cent drop 
            in 2003. Wataniya recorded an impressive 90 per cent growth in 
            post-paid subscribers but its overall post-paid subscriber base 
            stands at only a quarter of MTC-Vodafone's post-paid subscriber 
            base.<br>
            <br>
            Mobile penetration in Kuwait jumped from 28 per cent in 2000 to 
            reach an impressive 73 per cent in 2003. The result of the 
            competition has been a race to offer improved value-added services 
            and technologies, which ultimately will benefit the individual 
            mobile user, as well as the operators themselves, since these 
            services will surely enhance revenues and profits. A comparative 
            analysis by the AAG gives top marks to Kuwait's GSM rates. The 
            analysis correlates cellular rates to the wealth of the country (GDP 
            per capita). An important milestone since liberalisation of the 
            mobile market is the recent MMS interconnectivity agreement signed 
            by the two operators, enabling their customers to exchange 
            multimedia messages, regardless of which network they and their 
            correspondent subscribe to. By opening the gateway between the two 
            networks, the last of the restrictions facing customers in a 
            liberalised market has been removed. <br>
            <br>
            On the agreement, Wataniya Telecom CEO David Murray said: &quot;The 
            interconnectivity agreement between MTC-Vodafone and Wataniya is in 
            the interest of customers. It underpins the development of mobile 
            technologies and the services that the two operators provide to the 
            Kuwaiti market. Even though we remain staunch competitors, it makes 
            sense for us to work together for the benefit of our customers. <br>
            <br>
            Dr Saad Al Barrak, director-general of MTC-Vodafone, said: &quot;By 
            responding to our customers' requests, we expect to see a rapid 
            increase in the use of MMS services and growth in the uptake of MMS 
            handsets and creativity of the content-rich MMS in the Kuwait 
            market.<br>
            The Kuwaiti mobile market is seen as the model for healthy 
            competition in the Gulf region. Till 1999, Mobile Telecommunications 
            Company (MTC) had been the monopoly. Wataniya was then given the 
            licence for second GSM mobile operations. <br>
            <br>
            The Kuwaiti mobile telephony market is undergoing significant change 
            in the communication lifestyles of mobile phone users after the 
            launch of GPRS and MMS services, placing Kuwait on the forefront of 
            MMS developments in the region. &quot;We are now focussing on lifestyle 
            services for our customers, providing services that make their life 
            easier,&quot; says Haitham Suliman Al-Khaled, deputy general manager, 
            Business Development Group, MTC.<br>
            <br>
            Our cellular penetration is reaching saturation; so, the focus now 
            is on enhanced services. We recently introduced the SMS-Top-up 
            facility, which allows our customers to buy anything&nbsp; from 
            Internet time to pizzas&nbsp; via SMS, and the amount is debited to 
            the bank account directly,&quot; he added. <br>
            <br>
            MTC, founded in 1983, is the second largest listed company on the 
            Kuwait Stock Exchange (KSE) with a market capitalisation of nearly 
            $7 billion. New entrant Wataniya, which began operations only five 
            years ago, is traded on the KSE with a market capitalisation 
            exceeding $1.5 billion, with over 40,000 shareholders. MTC is a 
            strong contender for the second GSM operator licence to be awarded 
            by Saudi Arabia by the end of 2004. It has already launched its 
            service in Bahrain, along with continual deployment of the Atheer 
            Telecoms network in Southern Iraq, and maintaining its market lead 
            in Jordan (Fastlink). <br>
            <br>
            On MTC's regional aspirations, Al-Khaled states: &quot;We have 23 years 
            of experience in this field, and were the first private mobile 
            operator in the region. We can leverage this great reservoir of 
            experience regionally and we are eager to prove that we can serve 
            this promising market with our vision to cultivate and provide good 
            services.&quot; He feels MTC has an edge over its competitors, who do not 
            have a regional grounding. <br>
            <br>
            We understand specific needs of local customers because we have our 
            base in Kuwait, Bahrain and Jordan already. We can provide value 
            additions others can't. Saudi Arabia is our immediate concern. We 
            are also looking at UAE and, subsequently, Qatar.&quot; The company has 
            also bid for the second mobile licence in Oman, where bids are 
            currently being evaluated by the TRA from a reported shortlist of 
            six bidders. <br>
            <br>
            Meanwhile, Wataniya has also been expanding its base. Asia-Cell, a 
            consortium lead by Wataniya (40 per cent) was awarded the GSM 
            license for Northern Iraq. This licence is a crucial part of its 
            strategy to expand and develop its regional presence. It already has 
            a 50 per cent stake in Tunisiana, the second mobile operator in 
            Tunisia, and also operates the new GSM network in Algeria. <br>
            <br>
            The company has already pursued its intent to bid for licences in 
            Lebanon, which is expected to open its market to operators in 2005. 
            &quot;There are other clear regional opportunities in the Middle East 
            that we are pursuing strongly and we believe that Wataniya has a 
            compelling case to make,&quot; said Murray. As the company that brought 
            competition to Kuwait, Wataniya believes that competitive markets 
            have brought significant benefits to subscribers. &quot;When monopoly 
            markets have moved to a competitive model, we have seen vast 
            increases in mobile penetration, typically over 10 times that of 
            penetration under a monopoly. But we have also seen major movement 
            in the provision of advanced services aimed at providing subscribers 
            with easier, better, richer communications. The regional technology 
            leaders, in terms of the innovations that have made it into 
            consumers' hands, are the competitive markets, without doubt,&quot; 
            claims Murray. <br>
            <br>
            Launched in December 1999, Wataniya Telecom maintains that the 
            benefits of GSM competition for national economies in the Middle 
            East are not limited to growth in subscriber bases and richer 
            services, but also have effects on direct employment and opportunity 
            for local content developers and service providers. In addition to 
            investments in network equipment and the creation of jobs, the 
            company also sees major opportunities to support the growth of new 
            businesses and indirect employment through content and services 
            partnerships. &quot;We're looking at a three-way win here: developing 
            first-class telecommunications infrastructure for government, 
            services for people and businesses, and value for consumers,&quot; says 
            Murray. <br>
            <br>
