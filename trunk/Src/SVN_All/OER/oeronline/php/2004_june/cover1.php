                    </font><font color="#FF0000" face="Verdana">
            <font style="font-size: 13pt; font-weight: 700">GCC MOBILE TELEPHONY
            </font><font size="4"><br>
            </font><font style="font-size: 18pt; font-weight: 700">Sharing the 
            Spoils</font><font size="4"><br>
            </font></font><font size="4" color="#FF0000" face="Verdana">
            <br>
            </font><b><font size="2" color="#FF0000" face="Verdana">Competition 
            Calling</font></b><p align="justify"><b><font face="Verdana" size="1">
            The telecommunications sector in the Gulf is undergoing profound 
            changes. In the past few years, all six GCC states have set up 
            independent telecom regulatory bodies and have drawn up timetables 
            for privatisation of the fixed and mobile telephony markets. As it 
            happens, the mobile markets in most of the GCC countries have either 
            been already opened up for competition or are in the process of 
            opening up, presenting a host of opportunities for global giants as 
            well as local players with regional ambitions.<br>
            <br>
            </font><font face="Verdana" size="1">Vicky Kapur presents a 
            comprehensive analysis of the mobile telephony market in the GCC<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            R</font></b><font face="Verdana" size="1">ecent years have seen 
            profound developments in the economies of the Gulf Cooperation 
            Council (GCC) countries, with a switch towards privatisation and a 
            constant increase in the activities of extra-regional companies. 
            Some of the most exciting developments have occurred in the 
            telecommunications sector, which has witnessed the opening up of 
            large and previously inaccessible markets and ensuing phenomenal 
            growth rates. Local governments have increasingly come to attach 
            great significance to the extensive development of the telecom and 
            IT sectors. This has resulted in high State investment, easing of 
            restrictions on foreign entry and increased liberalisation. <br>
            <br>
            The capacity to provide telecommunications services in the region 
            has improved significantly over the past decades with the creation 
            of new infrastructure and fully digital systems. In general, the 
            relevant ministries of the countries in the region control and offer 
            most of the telecommunications services available there. Throughout 
            the GCC, telecommunications infrastructures are expanding and 
            modernising. Recognising the critical role of telecommunications in 
            economic development, governments have been pouring large sums of 
            money into expanding and improving local infrastructures. While the 
            ratio of telephone lines to population is still low when compared to 
            European countries, the fact remains that it is rising, and the 
            technologies being employed are in pace with the latest developments 
            in the West. <br>
            <br>
            However, with technology advancing rapidly, most have realised the 
            importance of competition and increased development for their 
            telecommunications sectors to achieve international standards. 
            Moreover, they are beginning to recognise that in this day and age, 
            telecommunications development is a necessary component for 
            continued economic growth. <br>
            <br>
            The problem in the region remains that since telecommunications is a 
            utility and subject to government control, those companies with 
            contracts in this area are confronted with administrative red tape. 
            As such, they are subject to the whims of ernmental entity 
            controlling them. As a result, there is a recent trend in the region 
            towards privatisation and development of the telecommunications 
            industry. <br>
            <br>
            Of late, private companies have been playing an important role in 
            providing businesses and affluent citizenry with alternative, 
            high-grade telecom solutions, offering services such as Internet 
            connectivity and value-added cellular services. Private industry's 
            role has even expanded into traditionally State-controlled 
            activities. Dissatisfaction with regular telephone systems led to 
            high popularity of cellular telephones in the region, and consequent 
            purchase and usage rates that are extremely high in comparison with 
            other regions. <br>
            <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>E</b></font><font face="Verdana" size="1">xperts estimate that, 
            in order for the industry to reach its full potential in the region, 
            an investment of at least $30 billion (RO11.5 billion) is needed. 
            While the GCC is clearly lagging in this area in comparison with 
            much of the rest of the world, it is making progress. It is 
            estimated that by end-2003, there were around 13 million mobile 
            subscribers in the GCC region, up from 10 million by the end of 
            2002. This number is expected to surge to 17 million by the end of 
            2005. The mobile market in the region has overtaken the fixed line 
            market, with the bulk of cellular subscribers belonging to the Saudi 
            Telecom Company (STC) and Etisalat of the UAE. STC had the largest 
            subscriber base of 7.5 million by the end of 2003. <br>
            <br>
            The penetration rate is considered an indicator of the country's 
            degree of market development. The UAE has the highest mobile 
            penetration rate of 74 per cent, followed by Kuwait (73%), Bahrain 
            (64%), and Qatar (63%). Saudi Arabia and Oman share the last two 
            slots, with penetration levels of 30 and 23%, respectively. The GCC 
            countries have a higher uptake of the mobile services compared to 
            other countries in the Arab region in line with their high per 
            capita income. The telecom sector is widely believed to be more 
            profitable than any other sector in the Arab world, with companies 
            making a profit in just the second year of operations. World 
            interest in the GCC telecommunications sector is expected to 
            continue to increase, creating more job opportunities and generating 
            increased revenue for governments. <br>
            <br>
            According to Jonas Lindblad, analyst (MENA), Pyramid Research, &quot;When 
            you look at the opportunities present in the GCC area, given the 
            high income levels in this region, the marketing efforts, in the 
            real sense of the word, have been quite crude in terms of service 
            offerings.&quot; Qatar is the richest market in terms of per capita GDP 
            ($31,544) followed by UAE ($19,593), Kuwait ($16,571) and Bahrain 
            ($11,730). Oman ($8,085) is stacked at the bottom of that cluster, 
            trailing Saudi Arabia ($8,742). &quot;The State-owned monopoly players 
            have, in general, lacked in terms of innovation in product offerings 
            and have not capitalised on the opportunities offered by customer 
            segmentation in the region,&quot; believes Lindblad. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>F</b></font><font face="Verdana" size="1">or instance, he 
            suggests, there is a huge non-Arabic speaking ethnic population from 
            south Asia - India and Pakistan in particular - in this region, 
            which isn't extremely comfortable with English either. However, none 
            of the monopoly players have come out with a customised product 
            offering lingual features, like sending an SMS in Hindi and/or Urdu, 
            to tap the potential that this segment offers. &quot;While the players 
            are not lacking in technology, they are indeed lagging behind in 
            terms of targeted and niche marketing. The mobile data segment, an 
            important revenue generator, has been hitherto unexploited in the 
            region,&quot; he claims. According to an IDC study, bucking the global 
            telecom slowdown, telecom, mobile, and data communications markets 
            in the Middle East &amp; Africa enjoyed high growth rates in 2003, 
            thanks in part to continued investments, the introduction of 
            competition in some markets, and regulatory reforms. However, 
            penetration rates suggest that the high growth phase of the past 
            years is now over and that churn will have a decisive effect on 
            competition between the operators in upcoming liberalisation drives.
            <br>
            <br>
            Customer loyalty plans, discounts and promotions, creative offers, 
            good service, dependability of the network and the provision of 
            content will be the main determinants of growth in subscribers' base 
            in the coming few years. The operators who can provide innovative 
            services, superior content, and high speed wireless connection are 
            the ones that will dominate the scene. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>A</b></font><font face="Verdana" size="1">s per the Arab Advisors 
            Groups Total Country Connectivity Measure (TCCM) in 2003, 
            <img border="0" src="naji.jpg" align="left" width="100" height="239">Qatar led 
            the pack followed by Kuwait, Bahrain and the UAE. The TCCM, which 
            defines the overall connectivity in a country, is calculated by 
            adding the household mainlines penetration, GSM penetration, and 
            Internet users penetration rates in each country. The household 
            mainlines penetration is measured by dividing the residential 
            mainlines by the number of households in each country. The TCCM 
            shows the extent of connectivity of individuals in a certain country 
            whether via fixed lines, GSM lines and/or Internet. <br>
            <br>
            Paradoxically, two of the leading four GCC countries - Qatar and the UAE&nbsp; are monopoly markets while Bahrain's cellular market moved 
            to duopoly last year. The UAE is followed by Saudi Arabia as far as 
            total connectivity is concerned, with Oman once again stacked at the 
            bottom of the rack. In fact, a drop in the number of fixed phone 
            lines in Oman negatively affected its household penetration measure 
            in 2003. <br>
            <br>
            Even though the UAE tops the list in terms of penetration, Etisalat, 
            the monopoly service provider, has been more of an infrastructure 
            provider than a service provider, by hosting other service-providers 
            applications on a revenue-sharing basis. In doing so, it hasnt taken 
            into account customer preferences, but has offered what was 
            available, claims Pyramids Landlady. A large part of its customer
            <img border="0" src="mobile_vs.jpg" align="right" hspace="5" width="300" height="197">base 
            comprises young, entrepreneurial populace, which can easily be 
            tapped for high-end applications. 'And this is something that the 
            second GSM operator&nbsp; whenever there is one - can easily exploit 
            by bringing in innovative and high-end offerings, he adds. For that, 
            however, the second operator will have to be financially strong - 
            perhaps a regional consortia. <br>
            <br>
            The governments decision to open up the mobile telephony segment to 
            a certain degree raises as many questions as it answers. The 
            countrys regulatory environment, for one, is not completely out of 
            the sphere of doubt. The chairman of Etisalat is also the chairman 
            of the regulatory body, which is akin to the kind of crossover 
            evident in Saudi Arabia, with Eng. Mohd Jameel Mulla being ernor of 
            the Saudi Communications Commission (SCC) - the Kingdoms 
            telecommunications sectors regulatory body - as well as the chairman 
            of the board of directors of Saudi Telecommunications Company (STC), 
            the countrys monopoly telecom service provider. <br>
            <br>
            Besides, there are other issues. One of the conditions that ernment 
            is expected to enforce on the probable entrant is that ernment 
            should be a majority shareholder in the entity, anticipates Lindblad. 
            Now that may not be too lucrative for a private global player, which 
            might have to then shell out the kind of royalties that Etisalat is 
            currently paying. According to its 2003 financials, Etisalat paid 
            royalties of up to 30 per cent of its profits to the government, 
            which is quite huge. &quot;The operations of Etisalat, it being a 
            monopoly operator, are very profitable. But for a newcomer to shell 
            out those kind of shares might not be feasible, 'explains Lindblad.
            <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">here are ample growth 
            prospects and business opportunities for mobile operators and the 
            equipment supply sector in the region. The awarding in April last 
            year of a second GSM licence in Bahrain was just the start of a 
            mobile services boom. The subsequent 13 months have seen huge sums 
            of money being poured into regional mobile telecoms and related 
            infrastructure and equipment investment. There is still tremendous 
            pent-up demand, planned privatisations, new licenses and increased 
            investment mandates by governments over the next few years. <br>
            <br>
            Investment is being made both in the expansion of existing networks, 
            whether monopoly or privately-owned, and in the 
            <img border="0" src="reg.jpg" align="right" hspace="5" width="250" height="227">construction of 
            entirely new networks. In December 2002, Pyramid Research estimated 
            that $3 billion (RO1.15 billion) could be invested in mobile network 
            infrastructure in MENA in 2003 and 2004 alone. That figure did not 
            include licence fees for new networks and potential M&amp;A costs. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>M</b></font><font face="Verdana" size="1">obile telephony will 
            continue to be a key driver of regional economic growth for some 
            time to come. With a combined population of 34,309,000 within the 
            GCC, 33 per cent are currently mobile phone users while 16 per cent 
            are using fixed line services, which is proof enough of the 
            potential of the region. The really big market that is being opened 
            up is Saudi Arabia. A tender for a second GSM license there was 
            issued in March this year, for which 11 consortia have reportedly 
            pre-qualified. The Kingdoms regulatory body, the Saudi 
            Communications Commission (SCC), is expected to announce the winner 
            of the licence by Q4 this year. A new and potentially interesting 
            development in Saudi has been that what is being awarded is a 
            =virtual licence, which will involve a new operator buying network 
            capacity from existing companies and offering service without having 
            to build a network. Saudi is another scenario, admits Pyramid 
            Researchs Lindblad. &quot;The liberalisation process is already up and 
            running in the country, and we should see an award (of the second 
            GSM licence) soon. However, quite a few question-marks still linger. 
            The regulatory environment of the country remains a key issue. The 
            STC has been a monopoly for years, and the preferential treatment 
            for the incumbent is something that the newcomer will have to watch 
            out for, he cautions. Besides, Saudi has a traditionally 
            old-fashioned market. &quot;The recent ban on camera phones in the 
            country has led to many a business consortia rewriting their 
            business plans, reveals Lindblad. This has dealt a severe blow to 
            those with plans to provide different kinds of value-added services 
            to customers in the country. The countrys mobile penetration rates, 
            at 30 per cent, are pretty low - so it is definitely a high 
            potential country with a huge population base of 22.5 million. <br>
            <br>
            The GSM boom in the Gulf has adversely effected on the regions fixed 
            line/payphone market. The payphones market is stagnating in some GCC 
            markets but is still growing in others. Moreover, fixed line prepaid 
            cards substitute the need for installing payphones. In Bahrain, for 
            example, the payphones service is struggling. In Saudi, however, the 
            influx of millions of pilgrims every year makes for an ideal context 
            for payphone services in the Kingdom. Saudi Arabia is the GCCs 
            leading payphone market - size and usage wise - followed by the UAE, 
            Bahrain and Qatar. The UAE, in fact, has the highest payphones 
            penetration rate (0.76 per cent) amongst the GCC countries, and its 
            payphones constituted 2.7 per cent of total mainlines. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>W</b></font><font face="Verdana" size="1">hile markets have been 
            waiting for the third generation (3G) mobile networks to be switched 
            on, attention is shifting now to a newer technology called fourth 
            generation (4G). This technology that was not expected to appear 
            before 2005 is already here. While first generation (1G) was analog 
            networks, and 2G was digital network like today's GSM (Global System 
            for Mobile), 3G is high-speed digital networks that let your mobile 
            phone do what your personal computer can do today, including, for 
            instance, getting e-mail, sending photos, watching video clips, 
            playing games, video conferencing and reading Web pages at much 
            higher speed. <br>
            <br>
            An intermediate technology to the 3G is 2.5G, commonly knows as GPRS 
            (General Packet Radio Service). GPRS is 'always on' and one does not 
            need to spend 20-30 seconds to establish a dial-up connection to the 
            Internet. The existing GSM networks in the region could be upgraded 
            at relatively low cost to provide GPRS services, including text and 
            multimedia messaging system (SMS and MMS) as many operators are 
            already doing. <br>
            <br>
            Mobile operators in Europe have spent about $100 billion (RO38.5 
            billion) buying licenses to run 3G networks, only to find that the 
            technology that most had agreed to use was harder to implement than 
            expected. Even where 3G networks are up and running, demand for the 
            video and multimedia services they make possible is still uncertain 
            and is being partially met by the less expensive GPRS service. 
            Expectations are being scaled down and 3G may end up merely as a way 
            for mobile operators to boost their capacity for voice calls in 
            overloaded parts of their networks rather than a goldmine of new 
            revenues from multimedia services. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>I</b></font><font face="Verdana" size="1">n the meantime, another 
            wireless technology, called Wi-Fi was developed to provide 
            high-speed Internet access to suitably equipped computers within 50 
            meters or so of a small base-station. It is being widely used in 
            homes, offices and universities in the US, Canada, Europe and Japan. 
            Several firms offer fee-based Wi-Fi access in airports, coffee-shops 
            and other public places known as hotspots. But because of the short 
            range of Wi-Fi technology, it is very difficult to provide universal 
            coverage. <br>
            <br>
            The 4G technology attempts to provide both high-speed wireless 
            networks and a wide area coverage. This is why it is nicknamed 
            nomadic broad band technology. It is seen as a rival to Wi-Fi 
            because it offers a wider coverage and is considered as a wireless 
            alternative to digital subscription line (DSL) that now provides 
            broadband access to homes and offices. Emphasizing speed first and 
            mobility later, 4G networks may be built initially in regions where 
            cable and DSL are unavailable. The mobile operators in the region 
            may consider skipping 3G and move directly to 4G. Unlike new 
            entrants, they already own the key sites for base-stations; so, they 
            are well-placed to build 4G networks fast once they get the approval 
            from the regulatory authorities to do so. 4G would make it possible 
            for mobile operators to attack the broadband market as well as 
            neutralise the Wi-Fi threat. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>M</b></font><font face="Verdana" size="1">obile phones and their 
            SMS and MMS services have already changed the habits of mobile 
            users. Downloading ring tones and logos had prompted the first 
            tentative use of phone bills to pay for other small services. The 
            next step will be to pay in the same way for Internet content and 
            other goods and services. More than a third of mobile subscribers in 
            Japan have used their phones to buy such goods as CDs, concert 
            tickets and karaoke songs with lyrics that bounce across the screen 
            and there is no reason why, with time, this trend will not take hold 
            in the region as well. <br>
            <br>
            Mobile operators will succeed if they shift their strategy from 
            being simply a voice channel to becoming an entertainment as well as 
            transaction and distribution channel for goods and services. MMS and 
            high-speed wireless connection covering a wide area will generate 
            added revenues and give mobile operators dominance over e-commerce. 
            The region needs companies to specialise in providing content to 
            mobile subscribers - mainly the young, who constitute more than 50 
            per cent of the population. This will allow operators to benefit 
            also from sizeable advertising revenues as they become the main 
            channel of Internet access. The growth in the number of mobile 
            subscribers and the higher revenue per user generated from the 
            additional services provided should help boost profits of mobile 
            operators in the coming few years. <br>
            <br>
            Research conducted by the Arab Advisors Group in 2003 revealed that 
            Kuwaits GSM market leads the GCC market in low cost rates, relative 
            to the examined countries' GDP per capita. The UAE and Bahrain are 
            close contenders with Oman and Saudi Arabia coming behind. Qatar 
            straddles the middle ground. On the post-paid average minute rates 
            front, Oman had the highest post-paid minute rates, followed by 
            Saudi Arabia, Qatar, Kuwait, Bahrain, and ending with the lowest 
            average minute rate in the UAE. The rankings change a bit for 
            prepaid minutes cost: Saudi Arabia has the highest rate, followed 
            Qatar, Oman, Kuwait, Bahrain, and ending with the lowest average 
            minute rate in the UAE. <br>
            <br>
            The opening up of Omans mobile telephony market for the award of a 
            second GSM licence is definitely a good opportunity for 
            international as well as regional players. However, the fact remains 
            that the country's economy is not doing too well. The macroeconomic 
            forecast for the country shows a further decline in its performance 
            in the near future. Unfortunately, GSM usage is linked to the 
            household income - a decline in household income levels necessarily 
            leads to a lower usage of mobile services, notes Lindblad. Moreover, 
            the fact remains that Omantel, the incumbent, has been slow in terms 
            of service offerings and sluggish in getting new products in to the 
            market, he adds. This, obviously, will provide a great opportunity 
            to the new player. <br>
            <br>
            <i><b>World mobile subscriber base <br>
            <img border="0" src="mob_sub.jpg" vspace="5" width="200" height="133"></b></i><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>I</b></font><font face="Verdana" size="1">n 2003, the world's 
            mobile user base grew by over 17 per cent to reach a total 1,354 
            million subscribers. More than a quarter of the year's new 
            subscribers were in China, which continues to report 4-5 million new 
            users a month. Overall, it was developing countries that accounted 
            for three quarters of the increase. In terms of value, the world 
            mobile services market reached a total 468 billion euros (RO215 
            billion) in 2003, up 11.3 per cent over last year. <br>
            <br>
            The rise in value derives largely from advanced countries which, 
            although reporting lower subscriber base increases, are home to 
            users who generate much higher average revenue per user than in 
            emerging countries, earnings which are now stabilised and even up in 
            some cases. Data revenues now account for 15 per cent of sales in 
            Western Europe, the most developed of telecom markets. <br>
            <br>
            The globe's base of mobile subscribers is expected to rise 
            significantly over the coming years, driven still by the development 
            of emerging countries, to reach close to 1,800 million subscribers 
            at the end of 2007 from the 1,354 million estimated at the end of 
            last year. The market's value over this period will rise to 615 
            billion euros (RO283 billion), representing 44 per cent of the 
            entire telecom services market's earnings. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he various sub-sectors 
            of the mobile telecom equipment market should see highly contrasting 
            structural evolutions through 2007. As a matter of fact, this market 
            accounts for nearly 30 to 35 per cent of global world telecom 
            equipment market, the remaining 70-65 per cent corresponding to fix 
            and long-haul markets, as well as corporate LAN equipment markets.
            <br>
            <br>
            According to IDATE estimates, world mobile services markets amounted 
            to $387.7 million (RO149.12 million) in 2002, which represents 4.5 
            times the volume of the mobile equipment market. According to IDATE 
            projections, this ratio becomes a 5.8 times ratio in year 2007. This 
            situation underlines the focus from mobile network operators towards 
            investment reduction and cash out restrictions. <br>
            <br>
            The year 2003 was the stage for a solid revival of the mobile 
            handset market in terms of units sold (510 million), and confirmed 
            the emergence of South Korean manufacturers Samsung and LG 
            Electronics. Despite a high volume of sales, however, the market's 
            value was affected by dropping prices - a direct consequence of the 
            competition come from Asia producers (Korea, Taiwan, China). For 
            European manufacturers, the euro's current high constitutes an added 
            problem in this area.<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>O</b></font><font face="Verdana" size="1">n the West's equipment 
            renewal markets, 2003 meant the onset of sales for handsets equipped 
            with a colour screen and with built-in features like a digital 
            camera or an MP3 player. In Japan, 94 per cent of the mobile phones 
            sold in 2003 were equipped with a digital camera. On the high-end 
            segment, then, sales in 2004 are likely to sustain the trend and 
            include even more advanced features such as built-in cameras that 
            can record up to one hour of video (LG Electronics) and high 
            capacity SD Flash memory cards. The fact that these devices are 
            incorporating an ever-growing number of CE features gives a clear 
            advantage to the Asian conglomerates that master these technologies 
            (from semi-conductors to screen production, by way of processors and 
            CMOS systems), namely Sharp, NEC and Fujitsu in Japan and Samsung 
            and LG in Korea. If demand is high, the other handset producers 
            could face component supply problems. <br>
            <br>
            Ho<img border="0" src="global.jpg" align="left" width="200" height="229">wever, in spite of the situation, several Western manufacturers 
            (e.g. Siemens and Sagem) have managed to hold their own even if, in 
