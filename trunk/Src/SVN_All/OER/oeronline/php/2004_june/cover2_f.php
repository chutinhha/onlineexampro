            <p align="justify"><font face="Verdana" size="1">
            <b>Qatar has the dubious distinction of being the least deregulated 
            market in the GCC, with no stated plans for the introduction of 
            competition in any telecom market segment <br>
            <br><center>
            <img border="0" src="table_qatar.jpg" width="397" height="191"></center>
            </b>
            </font>
            <p align="justify"><font face="Verdana" style="font-size: 11pt">
            <b>Q</b></font><font face="Verdana" size="1">atar's incumbent Q-Tel 
            is also the industry regulator. Despite having by far the highest 
            GDP per capita of the GCC countries, Qatar has the lowest mobile 
            penetration and the second lowest Internet penetration. Q-Tel is 
            also the most profitable of the firms listed on the Doha Stock 
            Market, with 2003 net profits exceeding QR1.14 billion ($313 
            million). It is also listed on the Abu Dhabi Stock Exchange since 
            2002. This accounted for over a fifth of the collective profits of 
            the 27 listed companies - little surprise that there is no immediate 
            plan to liberalise this small but rich telecom market. <br>
            <br>
            However, this has not been an easy achievement. After the 1998 IPO 
            for a 45 per cent stake in Q-Tel, the share price tanked below the 
            issue price of QR60 ($16.5) and it took some months to recover. But 
            since then, the share price has strengthened and proved an 
            exceptional performer compared with the recent global rout of 
            telecom stocks. For, Q-Tel has low debts and is investing in a major 
            programme to expand its infrastructure. The most profitable of all 
            sectors is the wireless service, as is the case in the rest of the 
            GCC. <br>
            <br>
            Q-Tel has set its sights on expanding its regional base, eyeing the 
            Oman market to implement its immediate plans. Earlier this year, 
            Q-Tel signed an agreement with Denmark-based telecom firm TDC for 
            cooperation in various fields, primary amongst which was working 
            jointly for the second public mobile network licence in Oman and a 
            joint venture company for investments in the telecommunications 
            industry. <br>
            <br>
            Although no public statement was made on bidding for the Oman 
            licence, local newspapers quoted a spokesman as saying, 'Q-Tel has 
            decided to expand its operations outside Qatar and, hence, wanted a 
            partnership with a major player in the industry which had a proven 
            record of international experience. TDC has, over the past 10 years, 
            made significant international investments in existing and 
            Greenfield operations in Europe and more than half of its revenues 
            now come from foreign operations.&quot; According to the agreement, the 
            companies will explore possibilities of cooperation on a 
            case-to-case basis in one-another's geographic zones - clear 
            evidence that Q-Tel is interested in more than just the GCC and Arab 
            markets. Q-Tel has already taken small stakes in telecommunications 
            satellites such as Thuraya. But the lack of the ability to purchase 
            interests in most State-controlled regional ventures has been a 
            problem. This, however, is slowly changing as the region opens up 
            through privatisation and the encouragement of inward investment.
            <br>
            <br>
            Q-Tel Chairman Sheikh Abdulla Bin Mohammed Bin Saud Al-Thani has 
            said that the agreement with TDC would enable it to &quot;expand its 
            activities internationally together with a partner with extensive 
            knowledge of operating internationally in the mobile industry.&quot; The 
            company had also initiated a two-year corporate restructuring effort 
            called Q-Turn in 2002. This initiative aimed at turning Q-Tel into 
            the most efficient telecoms company in the region. Developed by US 
            management consultants McKinsey, this is centred on optimising 
            products and service to clients. Meanwhile, the sole Qatari telecom 
            provider is raking it in. <br>
            <br>
            The chairman announced Q1 results for 2004 attributing much of the 
            profits to wireless services. 'Q-Tel achieved strong results with 
            revenue growth up by 20 per cent and net profit recording an 
            increase of 26 per cent over the corresponding period last year. The 
            performance was boosted by wireless and wireline services growth and 
            reflects the successful introduction of new products and value-added 
            services, coupled with a series of new customer promotions and 
            continuous commitment to improve services, which are central to our 
            QTurn transformation programme.&quot; The first quarter revenues 
            increased by QR95 million ($26.1 million) to touch QR 565 million 
            ($155.22 million) - an increase of 20 per cent over the same period 
            in 2003. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>W</b></font><font face="Verdana" size="1">ireless services 
            continued to spur revenue growth with Qatarnet GSM witnessing an 
            increase of some 40 per cent from January 1 to March 31. Q-Tel now 
            has more than 400,000 subscribers connected to Qatarnet. The service 
            accounted for a 29 per cent increase in wireless services in Q1 
            compared to the same period last year. The number of customers using 
            the Hala (pre-paid) GSM service shot up by 64 per cent from March 
            last year to March 2004 - an net increase of 100,000 new mobile 
            phone connections. The number of post-paid subscribers increased 
            during the year by 140,000. The wire-line services also experienced 
            a growth of 13 per cent in Q1 and there has been tremendous growth 
            in internet services too. <br>
            <br>
            Wireless services were launched in 1994, and it was the first in the 
            region to provide GSM (digital) network operations. Being an 
            absolute monopoly, enjoying large profits, Q-Tel while investing 
            considerable sums in technology, has not quite caught up with 
            customer-centric services offered in the liberalised markets of 
            Kuwait and Bahrain. <br>
            <br>
            Technologically, it is on a par with most telecom companies in the 
            region, and ahead of most. It has already launched GPRS and is in 
            the process of launching MMS and 3G services. By 2006, in time for 
            the Asian Games, it hopes to showcase a full range of multimedia 
            services. In preparation for the launch of MMS services, Q-Tel 
            organised a training programmed for MMS Mobile Phone Dealers, to 
            train the retailers on how to set a handset with the embedded MMS 
            software so that it will be compatible with the service. In addition 
            to sending MMS to mobiles, this will also allow sending e-mails. 
            Sheikh Fahad Al-Thani has said: 'The functionality of the MMS will 
            be the starting point of a broader customer experience which will 
            pave the way for 3G use.&quot; <br>
            <br>
            Q-Tel recently selected Tecnomen to deliver a Multimedia Messaging 
            Service Centre (MMSC). As well as providing a wealth of 
            opportunities, the rapid growth of Qatar's economy also presents a 
            variety of challenges to Q-Tel. One major challenge is to ensure 
            that the services provided by Q-Tel are able to meet and exceed the 
            increasing demands of their subscribers. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>W</b></font><font face="Verdana" size="1">hether a second mobile 
            telephony licence will be allowed before 2006 is still a closely 
            guarded secret although some reports say 2005 should see a second 
            licensee starting operations. Q-Tel has monopoly rights till 2013. 
            However, it has recently made a commitment to make its GSM services 
            more affordable. Two of its most recent and appreciated services are 
            the Arabic chat and double SIM card facilities. Arabic chat is now 
            available to both pre- and post-paid subscribers, and is an addition 
            to the existing QChat which allows customers to join chat groups and 
            exchange SMSs. The customer's mobile number and true identity is not 
            revealed to other users. <br>
            <br>
            Muzdawaj (double or parallel) facility is available only to 
            post-paid members, and allows two SIM cards for a single number. 
            Q-Tel wireless marketing senior manager Sheikh Fahad in Jassim Al 
            Thani speaking on Q-Tel's profitability at a recent marketing 
            conference said that Q-Tel had learnt from mistakes made by service 
            providers. 'In the past, most providers focussed on providing 
            high-technology products to their customers, regardless of whether 
            these customers actually had a use for it. As a result, a large 
            number of these companies ended up with massive losses since 
            customers did not utilise that technology, on which millions of 
            dollars had been spent. It is essential that all service providers 
            first listen to what customers require and offer products that are 
            user friendly and meet their needs, while being cost-effective,&quot; he 
            said. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>H</b></font><font face="Verdana" size="1">e also stressed that 
            Q-Tel was taking steps to provide services that suit individual 
            needs rather than a single service that was expected suit the entire 
            market. Despite its efficiency and quality, a year down the line, 
            Qatar will be the only market in the GCC that will not have 
            liberalised its mobile telephony sector. Q-Tel sources say there is 
            no immediate need as the market is still small both geographically 
            and numerically, and a long way from reaching saturation. <br>
            <br>
            Operators such as Kuwait's Wataniya and MTC and UAE's Etisalat are 
            already shifting their focus from increasing penetration and voice 
            use to mobile data and content. Qatar (along with Bahrain) is 
            expected to follow this route in 2004. But with a cellular 
            penetration rate of just over 50 per cent, Q-Tel's immediate 
            concerns are attracting more subscribers. <br>
            <br>
