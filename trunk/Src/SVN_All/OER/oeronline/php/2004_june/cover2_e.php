            <b>With Etisalat's monopoly having ended in April this year, the 
            governments of Dubai and Abu Dhabi are negotiating with a foreign 
            partner to set up a new mobile telephony firm <br>
            <br><center>
            <img border="0" src="table_uae.jpg" width="401" height="193"></center></b>
            <p align="justify">The United Arab Emirates' sole provider of mobile telephony and 
            other telecommunications services is Etisalat, the monolith of Gulf 
            telecommunications companies. Established initially in 1976, its 
            monopoly was established by Federal Act 1 of 1991. The UAE was one 
            of the first countries to introduce mobile phones to the Middle East 
            in 1982. Digital GSM telephony services began in September 1994. 
            Etisalat invests heavily in CAPEX Ð fixed asset investments in a 
            given year. <br>
            <br>
            Figures from the government-controlled National Bank of Abu Dhabi (NBAD) 
            show that Etisalat's market capitalisation peaked at nearly Dh49.5 
            billion ($13.49 billion) at the end of 2003 compared with Dh38.7 
            billion ($10.5 billion) at the end of 2002. Continuing strong demand 
            and rising revenues boosted Etisalat's net profits by about 17 per 
            cent in 2003 to reach Dh2.8 billion ($763 millio), on a turnover of 
            Dh9.2 billion ($2.51 billion). Much of the profit is re-invested in 
            new developments. <br>
            <br>
            Etisalat is majority-owned by the government of the UAE with a 60 
            per cent holding of the shares. Individual UAE shareholders hold 
            most of the remaining 40 per cent although some foreign investment 
            funds have managed to find ways of owning Etisalat shares, according 
            to a recent report by the Economist Intelligence Unit. Etisalat is a 
            major source of revenue for the UAE federal government, with an 
            annual contribution of some Dh2.5 billion ($681 million), including 
            50 per cent of net profits provided directly to the federal budget Ð 
            the second largest contributor after the oil sector. Mobile 
            telephony services provide a substantial part of this revenue, given 
            the depth of penetration in the population as a whole. <br>
            <br>
            UAE Communications Minister Ahmed Humaid Al Tayer, speaking at the 
            launch of Etisalat's 3G service in December last year, said that 
            personal and business communication was entering an era of 
            connectivity, content and convergence. Whereas, in the past, 
            mobility was seen as an add-on, it was becoming a fundamental aspect 
            of many of Etisalat's services. <br>
            <br>
            Etisalat has not confined itself to operating telecoms in the UAE. 
            The company has also been bidding for a mobile telephone licence in 
            Saudi Arabia. <br>
            <br>
            Mobile penetration in the UAE has reached 79 per cent by Etisalat's 
            own estimate whereas fixed line penetration is only 30 per cent. 
            Given that mobile phone penetration averages 34 per cent in 
            developed countries and just 5 per cent in developing countries, the 
            high rate of mobile penetration in the UAE is a measure of the 
            relative wealth and maturity of the market. <br>
            <br>
            At 90 per cent usage, Dubai has the highest rate of mobile 
            penetration in the UAE. The Economist's city guide to Dubai names 
            the mobile phone as the Ômust-have' accessory. Abu Dhabi has the 
            next highest rate of mobile penetration at 62.5 per cent. Together, 
            the two emirates account for 74 per cent mobile phone lines in UAE.
            <br>
            <br>
            Despite the very high penetration of mobile telephony, the market 
            continues to expand at something like 10 per cent a year. Expansion 
            is fuelled by the popularity of SMS and multi-messaging facilities, 
            introduced in July 2003, which encourage consumers to buy new 
            handsets with the capability to take advantage of new technologies. 
            It is not unusual for some people to exchange their handset every 
            month. <br>
            <br>
            Etisalat offers basic post-paid and pre-paid mobile GSM phone plans. 
            The Wasel pre-paid plan, and the availability of cheap handsets has 
            proved a boon to the many expatriates who support Dubai's economy. 
            At the beginning of December 2003, Etisalat reduced the price of 
            international calls, including on the Wasel service, notably to 
            India, Pakistan, Sri Lanka, Bangladesh, Egypt, Iran, Syria, Lebanon, 
            Sudan and Yemen. Reduced tariffs have increased revenue, rather than 
            reduced it, since consumers make more and longer calls. <br>
            <br>
            International roaming is available on Wasel phones for incoming 
            calls only. Subscribers to the standard GSM service are obliged to 
            make a Dh500 ($136; RO52) deposit for the facility, which also 
            includes outgoing calls. In 2001, Etisalat signed an agreement with 
            Thuraya to initiate its satellite mobile service in the UAE. Thuraya 
            enables voice, data, fax, short messaging and GPS. In April last 
            year, Etisalat engaged Alcatel (France) to operate a 3G service in 
            the UAE providing subscribers with high-speed/high-quality mobile 
            multimedia facilities such as high speed data, video streaming, 
            music downloading, Web browsing, chat, online gaming and video 
            telephony calls between multimedia terminals (voice, images and 
            video). The service became operational at the end of 2003. <br>
            <br>
            A second agreement with Huawei Technologies (China) to supply UMTS 
            technology for 3G services was signed in December 2003. Commercial 
            launch of the 3G service is scheduled for the third or fourth 
            quarter of 2004. The 3G service, branded as the Mubashir plan, was 
            announced in January 2004, making the UAE one of the first countries 
            in the region to experience 3G mobile telephony.<br>
            <br>
            Etisalat's monopoly has ended. Federal Act 3 of 2004, published on 
            April 11, 2004, provided for the re-structuring of the 
            telecommunications industry in UAE. Two new bodies were created to 
            regulate entry of new companies into the market and to oversee 
            licensing and tariffs. <br>
            <br>
            The market responded negatively to the announcement. Etisalat's 
            shares fell 20 per cent by the beginning of May. On the other hand, 
            business leaders had claimed that ending Etisalat's monopoly would 
            result in better and cheaper services and new investment 
            opportunities. <br>
            <br>
            Uncertainty remains about the prospects for Etisalat. The decree 
            does not specify whether the company will be broken into components 
            for sale to bidders, or whether the government will retain its 
            infrastructure. Should Etisalat's commitment to pay 50 per cent of 
            its profits to government be conceded, then investors could receive 
            higher dividends. If government retains infrastructure, investors 
            can anticipate legal wrangling over how revenue is divided. <br>
            <br>
            A decision taken at the first meeting of the newly constituted 
            Supreme Committee overseeing the UAE's telecommunications sector 
            specified that the UAE government is to hold the same stake in any 
            new telecom service provider in the UAE as is the case with Etisalat 
            now. This means that new telecom firms will have to pay royalty fees 
            to the Federal Government. In addition, future licensing of telecom 
            service providers in the UAE will be regulated according to 
            international standards, and will follow the same system as that 
            applied to Etisalat. <br>
            <br>
            Industry analysts believe that Etisalat will hold its own, given its 
            infrastructure, experience and outlook, although double-digit profit 
            growth is unlikely in the short- to medium-term given the relative 
            maturity of the market and the likelihood of intense price 
            competition. Orascom, the Egyptian telecoms giant, which was granted 
            a licence to operate in Iraq earlier this year, is believed to be 
            interested in entering the market in the UAE. It has emerged that 
            the governments of Dubai and Abu Dhabi are negotiating with a 
            foreign partner to set up a new telecoms firm, which would sell 
            mobile telephony. The two governments would hold 30 per cent each 
            and the foreign partner 20 per cent, with the remaining 20 per cent 
            being offered to the public. <br>
            <br>
