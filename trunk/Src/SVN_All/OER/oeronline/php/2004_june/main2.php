<p align="justify"><font face="Verdana" size="1"><b>The popularity of online recruitment has increased considerably over the last 
few years. Vicky Kapur talks to Mukhtar M. Hasan, director, and Mrs Fairouz 
Asfour, marketing consultant, of the first fully bilingual Omani head-hunters 
site &#8722; <a href="http://www.headhunt-me.com">www.headhunt-me.com</a> &#8722;&nbsp; to get insights into the future of online 
recruitment in Oman </b> </font></p>

<p align="justify"><font face="Verdana" size="1"><b>How popular is online recruitment in this part of the world? </b><br>
<b>Mukhtar Hasan:</b> The use of online recruitment is still relatively new to 
the Middle East region, but its popularity has grown significantly over the last 
2-3 years. We believe this trend will continue for a number of reasons: <br>
<br>
<b>1)</b> Globally, Internet recruitment took off at the start of the 1990s and 
has demonstrated sustained growth in popularity. The massive success of sites 
such as Monster.com demonstrates the potential online recruitment has to 
revolutionise the way employers undertake recruitment and the way jobseekers 
search for jobs. <br>
<br>
<b>2)</b> There are already a handful of successful jobsites focusing on the 
Middle East region, with the market leader boasting over 400,000 jobseekers 
registered on the site.<br>
<br>
<b>3)</b> With Internet usage set to increase to over 25 million by 2005 in the 
Middle East (from a current estimate of 8 million users), we believe the uptake 
of employers and jobseekers using the Internet to find their next employee/job 
will also grow. The efficiencies Internet recruitment offers are impossible for 
employers or jobseekers to ignore. <br>
<br>
<b>Which category of jobseekers access online recruitment sites the most?<br>
Mukhtar Hasan:</b> The requirement of users to be basically com<img border="0" src="../2004_june/job_pic1.jpg" align="right" hspace="5" width="250" height="166" vspace="3">puter 
literate does, to some degree, shape the profile of the users of Internet 
jobsites. The largest categories currently using online recruitment tend to be 
those professions who are most comfortable using computers and the Internet and 
do so on a daily basis. For example, Internet jobsites have had a massive uptake 
amongst IT professionals. At Headhunt-Middle East, however, we are committed to 
making the benefits of online recruitment accessible to as wide a category of 
jobseekers as possible. We are, therefore, currently looking into ways we can 
promote this and this may in the future include initiatives such as Open Days 
where we invite jobseekers to come and meet with our team and let us guide those 
less confident with using the Internet through the process of searching for jobs 
and registering on our site. <br>
<br>
<b>What is the inspiration behind the site? How did the idea for Headhunt-ME.com 
come up?</b> <b>Fairouz Asfour:</b> It was initiated by one of the Headhunt-ME.com 
directors when he was at the Regional Board of the Harvard University, and one 
of the studies that he did was how can the Middle East best utilise among 
themselves the human resources available in the region, and why they are not 
using it properly. For instance, there are skilled people available in countries 
like Lebanon, Jordan and even Oman, and there could be a shortage of skilled 
manpower in Saudi Arabia, but the movement of employees within these countries 
was not happening. It was then that the idea for such a site came up. The basic 
objective of Headhunt-ME.com is purely to have regional movement of employees 
and help them land jobs wherever there is an opportunity for a person of their 
skill-set. <br>
<br>
If a person from New York can travel to Los Angeles to work in a field he is 
skilled in, I do not see why an Omani cannot just jump on the plane and work in 
Bahrain or Saudi Arabia. Besides, we have witnessed people from London going to 
Aberdeen or coming to the Gulf to work - why can't we have people from the Gulf 
going to London or Aberdeen to work? Headhunt-ME.com is not just for the 
citizens of the region, but it also for the large floating population of 
expatriates in the region. <br>
<br>
Earlier, the opportunities available to an expatriate who has worked in one part 
of the Gulf to look for a job in another part were few. But now he/she can use 
the site. In addition, it doesn't only help jobseekers; it also helps employers 
as well. Earlier, if we had to employ, say, an accountant, we had to go to India 
or Philippines or Pakistan. Now, if I want a person who already has Gulf 
experience, I can find the right person with the right experience with the help 
of this site. <br>
<br>
<b>Mukhtar Hasan:</b> Unemployment in this region is high compared to other 
parts of the world. The main reason for that is lack of intra-region mobility. 
As far as Oman is concerned, this could become a major foreign exchange earner 
for the country. The Sultanate has a big local workforce, and there are figures 
like 30,000 people coming into the job market every year, who have to have a way 
to find jobs. These potential employees could land up jobs in other parts of the 
region through this site and thus earn foreign exchange for the country. <br>
<br>
<b>What's your revenue model?<br>
Mukhtar Hasan: </b>The site is currently free for both the employers as well as 
the jobseekers. The idea is to initially get the revenues from the banner ads 
and the database services that we provide to companies. Once the site matures, 
we will try to charge a small administrative fee of $10 (RO4) to the jobseekers. 
Our aim is long-term, and we are looking at the volumes that will make it 
happen. <br>
<br>
The reason we’re not charging the employers is to make it easier for them to 
register and advertise for jobs with us without having to think twice. Once the 
jobseekers are satisfied that they are getting enough opportunities through the 
site, they will definitely not mind paying the nominal administrative fee, and 
that will help us become profitable. Moreover, since the site is being managed 
on a very professional basis and everything is computerised, our cost base is 
low. That helps when you have a long-term perspective. <br>
<br>
<b>Are employers in the Middle East relying on Websites to attract ight talent?<br>
Fairouz Asfour: 
</b>Yes, employers in the Middle East are increasingly relying 
on sites such as Headhunt-ME.com to attract the right talent and this is for a 
number of reasons: <br>
<br>
<b>1) </b>Businesses around the world and equally in the Middle East are 
becoming increasingly aware that their most valuable business asset is the 
people they employ. Sourcing the right talent is, therefore, becoming a key 
issue for employers. Sites such as Headhunt-ME.com enable employers to cast 
their recruitment nets wider than ever before; using the Internet to access a 
global pool of jobseekers and identifying skilled candidates that may never have 
come to their attention using traditional methods of recruiting. <br>
<br>
<b>2)</b> Internet recruitment is also considerably cheaper than traditional 
recruitment methods such as advertising in local newspapers or retaining a 
recruitment consultant to run a recruitment campaign. In the case of Headhunt-ME.com, 
the services we offer are absolutely free for employers and this means that 
employers can post as many jobs on the site as they need and keep them there as 
long as needed. With no financial restraints, employers can afford to search for 
the right candidate for as long as necessary, rather than taking the best 
available candidate (though not necessarily the right one) from a short and 
expensive recruitment campaign. <br>
<br>
<b>3)</b> The Internet also provides efficiencies and timesaving practices that 
were not previously available through traditional recruitment methods. As 
companies come under increasing pressure to cut costs and time, the efficiencies 
offered by Internet recruitment sites become increasingly attractive. Using 
Headhunt-ME.com for example, a HR Manager at a company can search our CV 
database (using our sophisticated search filters to ensure he/she only see CVs 
relevant to the position the company is looking to fill) and contact a handful 
of jobseekers in minutes. Alternatively, he/she can post their job vacancy and 
simply wait for CVs to be delivered to their inbox. <br>
<br>
What has been the response from Oman? Fairouz Asfour: These are still early days 
to gauge a full response to the service. However, we have presented the site to 
a number of leading companies in Oman and discussed it with numerous individuals 
and we’re pleased to say that the response has been 100 per cent positive. We 
designed the site with the aim of making it easier for jobseekers to search for 
and apply for jobs across the region and to make it faster and more efficient 
for employers to find the right employees for their company. Everyone we have 
spoken to has felt that this will be extremely helpful for Oman. <br>
<br>
<b>What makes Headhunt-ME stand out in a deluge of online recruitment agencies?<br>
Mukhtar Hasan: 
</b>We believe there are a number of key points that distinguish 
Headhunt-ME.com: <br>
<br>
<b>1) </b>There are few online recruitment services that target the Middle East 
region specifically. The majority of the recruitment services you’ll find online 
target the US or European markets. We wanted a site that specifically helped 
people looking to work in the Middle East. <br>
<br>
<b>2) </b>Our service is free for employers in contrast with the majority of 
sites that charge companies large fees to post jobs and search for candidates. 
This will enable smaller companies or those with no recruitment budget to 
benefit from the service. This is also extremely beneficial for jobseekers, as a 
no-fees policy will encourage a wider range of companies/organisations to use 
the service and provide jobseekers with a wider range of employers to apply to.
<br>
<br>
<b>3)</b> Headhunt-ME has been designed with an emphasis on ease and efficiency 
of use. We wanted even the most basic Internet user to be able to post their CV 
and search for jobs. Our design is simple, aids navigation and is quick to load. 
We have information rich content to assist jobseekers in looking for a job, 
including a career advice section and country profiles for people looking to 
move to a new country. Many jobsites look nice but lack helpful information, are 
confusing to use and take too long to load. <br>
<br>
<b>So aren't you directly competing with the brick-and-mortar consultants?</b>
<br>
<b>Fairouz Asfour:</b> On the contrary, we are complementing the recruiters. We are 
allowing recruiters to advertise and look for jobs on the site. In fact, a 
recruiter could put online the database of CVs that he might have, instead of 
putting it in his drawer where nobody can see them, and receive enquiries. <br>
<br>
<b>How many people have landed jobs through your site yet?</b> <br>
<b>Mukhtar Hasan:</b> We already have a few success stories where people have got jobs 
through our Website. I remember the first one a few months ago - there was this 
accountant who was working here in Oman who got an excellent job opportunity in 
the UAE, whereby he tripled his salary! He came to our office and took one of 
our staff members out to dinner. <br>
<br>
<b>Why did you opt for a bilingual site (Arabic &amp; English)?</b> <br>
<b>Mukhtar Hasan: </b>Primarily, our aim is to assist Arabic speaking jobseekers in the 
Middle East region looking for the right job and finding the potential employer 
in a more convenient, professional and effective manner. The same applies for 
Arabic speaking employers in finding the required personnel. Therefore, it was 
agreed and decided to offer a complete bilingual online recruitment Website that 
has a full-fledge operating system which could be used in both English and 
Arabic. Our market leading booster is to reach a wider candidate pool, employers 
and recruiters without any failure and to offer them the opportunity to search 
and apply for jobs/CVs in Arabic and English language. </font></p>

