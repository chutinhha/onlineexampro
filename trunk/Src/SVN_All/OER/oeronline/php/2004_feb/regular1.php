                  <font face="Verdana" size="1">
            <b>Chevrolet claims the 2004 Optra gives you 'more car, for less'. 
            After a smooth, quiet, and spirited test-drive, Nayl D'Souza confers 
            that this new Chevy is worth the money and could save you on fuel 
            bills<br>
            <br>
            <img border="0" src="car.jpg" align="left" hspace="5" width="250" height="172"></b>After 
            eyeing the lower end of the car market in the Middle East for some 
            time, General Motors (GM), the world's biggest auto maker, has 
            decided to pounce. Already renowned for sporty models like the 
            popular six-cylinder Lumina in the midsize category, GM's flagship 
            Chevrolet brand is aiming to enjoy equal success in the lower medium 
            price bracket that is crowded by buyers. The only thing likely to 
            get in its way is complacency. Unit sales in this segment represent 
            about 35 per cent of all car sales in the regional market. What's 
            more, pundits forecast that this market tier is set to double by 
            2008.<br>
            <br>
            Oman Trading Establishment (OTE), Chevy's dealership in the 
            Sultanate, introduced new cars to the market at the turn of 2004 
            with the resounding slogan 'The Force of Four'. A wide range of 
            models to suit needs and budgets. The local showroom welcomed the 
            1.5-litre hatchback Aveo, the 2.0-litre Epica and the Caprice, 
            available in 3.8 and 5.7-litre engines. Plus, the all-new Optra was 
            introduced into the fold. <br>
            <br>
            The Optra is available with a 5-speed manual or a 4-speed automatic 
            transmission in Oman. You can choose between an LX (base level), LS 
            (mid-package) or LT (top-of-the-range) model with two engine options 
            available: a 1.6 or 1.8-litre engine.<br>
            <br>
            In all fairness, there are plenty of good reasons for getting behind 
            the wheel of the Optra as many consumers around the world have 
            already discovered although the car's run-of-the-mill appearance is 
            unlikely to stop traffic. For example, it has struck the right chord 
            with consumers in India and Malaysia following its summer release in 
            2003 in these countries. Indeed, the Optra LT recently scooped Car 
            of the Year on Wheels, the Asian weekly automobile show broadcast on 
            BBC World. <br>
            <br>
            Such accolade was enough to entice me to accept the invitation of 
            OTE to take the 1.6-litre LS out for a spin find out whether we were 
            likely to see the Optra become a prominent feature on Oman's roads. 
            The thought of driving such a small-engined and timid-looking Chevy 
            was not very appealing but I began my road test with an open mind 
            and was encouraged to discover for myself why such a car had gained 
            so many admirers despite fierce competition. With a starting price 
            of RO3,645, the 1.6-litre Optra is pitted against the likes of the 
            new Honda City while the bigger 1.8-litre model (which starts from 
            RO4,900), goes head-to-head with popular rivals such as the Toyota 
            Corolla and the Nissan Sunny. <br>
            <br>
            <b>Exteriors </b><br>
            The Chevy's bow-tie logo is perhaps the most striking feature of the 
            Optra from a frontal view but the side and rear profile offer rising 
            shoulder lines, an angular wrap and a cluster of attention grabbing 
            jewel-like tail lamps. There may not be enough solid lines for some, 
            but there are enough to make the curves look tough and sporty. It 
            comes in a reasonable range of 10 colours and if green is your 
            favourite you're in luck as you can choose from a modern, mint or 
            khaki tone.<br>
            <br>
            Interiors Standard features on the Optra LX, include power steering, 
            front power windows (which is a feature usually overlooked on a 
            lower end medium saloon), high-output air-conditioning, central 
            locking, and cloth trim. You can add options such as front and rear 
            power windows among other extras. However, leather seating (which 
            few price-conscious buyers demand) will dent your wallet by at least 
            a further RO400. It comes as an option in the deluxe LT which is 
            fitted with electronic climate control, dual-stage driver and 
            passenger air bags, remote keyless entry, wood trim and steering 
            wheel volume controls for an in-dash 5-CD changer audio system.<br>
            <br>
            The Optra has a functional, visually attractive interior although 
            the textured plastic used on the dash is less enthralling than the 
            fabrics on the seats. Most controls are laid out in a logical and 
            ergonomic manner. <br>
            <br>
            The boot might not be big enough to swing a Bengal tiger but it does 
            stow a barrage of suitcases. Indeed, there are no shortage of 
            storage areas in the car. For the driver there is a coin holder 
            underneath the steering wheel as well as a sunglass holder by the 
            sun visor. The front passenger has in reach a sizable glove 
            compartment and a tray under their seat. The tray is smaller in 
            width and length than a copy of OER but deep enough for some toy 
            racing cars. A special button to prevent children in the back from 
            using their power windows (a kind of anti-pinch device I guess) is 
            close at hand to the driver. Cup holders on the armrests to store 
            milkshakes on the move and secret locks on the rear doors to ensure 
            the kids stay put make the car even more family-friendly. Yet, once 
            inside, probably the most pleasing feature of the Optra is its level 
            of spaciousness. For a car in this segment, rear occupants get 
            plenty of legroom: 932mm to be exact.<br>
            <br>
            <b>Performance</b><br>
            Under the hood of the Optra, there's a DOHC in line four cylinder 
            16-valve engine. The base model LX with its 1.6 litre engine is 
            mated to a five-speed manual gearbox that produces 107 HP at 5,800 
            rpm. It has a top speed of 175km/h. The 1.8 litre power plant with 
            four-speed automatic transmission produces more torque and 199 HP at 
            6,000 rpm yet it generates the same top speed and cannot eclipse the 
            smaller Optra's acceleration from 0-100km/h in 12.4 seconds. A good 
            reason, then, to choose the smaller engined Optra or the 1.8-litre 
            as a manual. <br>
            <br>
            This said, the Optra isn't sluggish but don't expect too much verve 
            either. It can hold its own in traffic although it is just as happy 
            chugging along at 60 km/h as it is speeding at 120 km/h. Sensibly 
            weighted light easy controls, steering and gearbox make it easy to 
            drive. The all-disc brakes seem give the grip and sharpness you 
            require for Muscat driving. <br>
            <br>
            In the past, the sight of a Chevy badge may have chased away some 
            fuel-conscious customers. The Optra, however, has earned a 
            reputation as a car that sips on gas. Even with the air conditioning 
            on and driving like a raving loony, you can expect to match the fuel 
            efficiency rating that the car maker believes it will deliver. It 
            insists that the Optra will offer fuel efficiency of 8.1 litres per 
            100km on combined driving conditions with an automatic. <br>
            <br>
            The Optra is quiet to drive. The engineers appear to have done a 
            good job of using hydraulic (oil-filled) mountings for the engine 
            and transmission to cut noise and vibrations effectively. 
            Apparently, connecting the alternator, power steering pump and 
            air-conditioning compressor with a single auto-tensioned drive belt 
            has further helped. <br>
            <br>
            <b>Safety</b> <br>
            The safety features that the Optra LX comes with include a 
            high-tension steel safety frame, strategically located steel crash 
            bars, front and rear crumple zones, collapsible steering and height 
            adjustable three-point safety belts for all passengers, including a 
            driver seatbelt warning chime. As mentioned, Chevy also offers 
            airbags for driver and passenger on the LS and LT variants. <br>
            <br>
            <b>The finishing line </b><br>
            Chevy officials insist that the Optra has several more international 
            aspects attached to it than are usually discussed. &quot;Chevrolet took 
            the best designers from Giorgetto Giugiaro and created this 
            great-looking car, inside and outside,&quot; boasts one salesman. Another 
            enthuses that while Italians did the styling, the car's engines 
            originate from Australia, the Germans had a hand in partnering the 
            engine with an automatic transmission system and Lotus Engineering 
            even pitched in by fine-tuning the suspension. But regardless of 
            where the car was designed and who is ultimately responsible for its 
            construction, there is no question that the Optra competes best on 
            price. Adequate power using less petrol, a quiet ride and generous 
            amounts of cabin space are nice for the affordable price but given 
            its extensive specifications to boot, it is one of the most sensible 
            deals in town and great value that few can contest.</font></p>
