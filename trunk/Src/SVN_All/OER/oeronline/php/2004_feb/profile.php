                    even his thoughts on space exploration to Nayl D'Souza<br>
                    <br>
                    <img border="0" src="profile.jpg" align="left" hspace="5" width="190" height="220"></b>Picture 
                    this. You are driving towards a staggering cement plant 
                    nestled amidst rugged mountains that are a stone's throw 
                    outside of Rusayl industrial area. The landmark is a stark 
                    reminder of Oman's drive towards self-sufficiency in core 
                    industries. A freak gust of wind howls around the car as you 
                    reach the security gate to the annoyance of the guards on 
                    duty. You proceed with haste to the shelter of the 
                    administration building but get pummelled by the wind. You 
                    head left and walk towards the end of a long corridor. Here 
                    you find a secretary who guesses correctly who you are. He 
                    announces your arrival to the company's chief executive who 
                    you are due to meet. A moment after regaining your poise, an 
                    escort whisks you through a smaller corridor, past a set of 
                    wooden double doors and into a luxurious 
                    office-cum-boardroom with a huge table and an ergonomic 
                    desk. You survey the rest of the room. As you gaze around 
                    you are greeted by a good view of the magnificent factory to 
                    the north and a couple of cabinets to the east which are 
                    crammed with many glittering His Majesty's Cup trophies (the 
                    firm has bagged 10 HM's Cup wins since 1991). One tall 
                    shelving unit is also adorned with jars of sediment that you 
                    suppose are ingredients for cement and you can't help but 
                    notice lots of colourful stones inside cabinet frames hung 
                    on the walls. Finally, but by no means last, you glance to 
                    the west where the outstretched hand of a smiling Jamal 
                    Shamis Al Hooti officially confirms your warm welcome to 
                    Oman Cement Company (OCC). <br>
                    <br>
                    Al Hooti, 50, is impeccably dressed, well-groomed and 
                    fighting fit. It comes as little surprise to learn later 
                    that this may have something to do with his enjoyment of 
                    swimming and mountain climbing as well as tennis. It could 
                    also be a sign of his easygoing nature and the fun he has 
                    away from the office with his wife and six children. 
                    Regardless of the secrets behind his youthful looks, he 
                    instantly builds a rapport with his personable, professional 
                    and cheerful manner. <br>
                    <br>
                    &quot;My job is to make sure that our business maintains its 
                    market share in Oman and level of exports to UAE as the 
                    company considers its expansion programme,&quot; says Al Hooti. 
                    It's a tough assignment as competition will be stiffer than 
                    ever. <br>
                    <br>
                    &quot;In the long term, such expansion of the cement industry is 
                    warranted by virtue of steady rises in population and 
                    economic growth in Oman and many parts of the Arabian 
                    peninsula. There is a shortage of cement within the GCC and 
                    other companies in the region have already begun expansions 
                    in Saudi Arabia and Qatar. Yemen is looking forward to 
                    growing its domestic industry, too. <br>
                    <br>
                    &quot;Like others, our company is waiting for the full results of 
                    Census 2003 to be released. The age of Oman's population is 
                    of great importance to us. The demographics of the country 
                    will determine future demand of cement and we will need to 
                    fine-tune our strategy accordingly. <br>
                    <br>
                    &quot;We do anticipate an expansion in the future because of 
                    population growth. Whether this means a new kiln in our 
                    existing plant or the setting up of a new cement factory 
                    remains to be seen. Setting up a new plant will require 
                    financial support from Government. From a macro-economic 
                    point of view, this new infrastructure will create new jobs 
                    and deliver more wealth to the nation.&quot; <br>
                    <br>
                    <b>Bright outlook </b><br>
                    These are halcyon days for Oman's cement industry. During 
                    2003, OCC produced over 10 per cent more clinker (the 
                    material that emerges from a cement kiln after baking which 
                    is ground with a small amount of gypsum to produce cement) 
                    than the plant was designed to make and sold over 1.6 
                    million metric tonnes (MT) of cement it had manufactured 
                    which represented the highest quantity of cement ever sold 
                    by the company since its inception in 1983. Currently, OCC's 
                    order books for exports are full and infrastructure projects 
                    (both locally and across the border) should result in 
                    greater demand and higher sales in the near future. <br>
                    <br>
                    But Oman's cement industry hasn't always been rosy, explains 
                    Al Hooti. &quot;In the past, huge customs duties as high as 50% 
                    existed in Oman in the late 1980s along with quotas. &quot;In 
                    1991, these barriers to foreign imports were taken away and 
                    the domestic industry had to fend for itself.&quot; The plant 
                    only had a 2,000 MT per day capacity, cement prices were 
                    depressed, and profit was below RO5 million. If protection 
                    against cheap foreign imports was not available, how could 
                    the company possibly survive and improve its bottom line?
                    <br>
                    <br>
                    &quot;We took drastic steps,&quot; reveals Al Hooti. &quot;Oman Cement 
                    Company decided to price the product at a more reasonable 
                    level to reflect the availability of resources needed to 
                    produce it. This was not a popular decision and was fiercely 
                    contested by some market players and the general public. <br>
                    <br>
                    However, they began to appreciate the true value of 
                    Omani-made cement, the profits of which were being ploughed 
                    back into developing infrastructure of the nation, raising 
                    efficiency at the plant and making the product readily 
                    available in the future.&quot; <br>
                    <br>
                    The fact that OCC has been so successful in beating 
                    competition from much larger regional producers and tackling 
                    anti-dumping laws without any government support is a clear 
                    indication of how geared up to meeting the challenges in 
                    cement marketing the company has been. Today, this is still 
                    being achieved through a combination of production 
                    efficiency, pricing strategy and customer support. It 
                    appears that all companies go through cycles, but only the 
                    best ones come out stronger. Al Hooti has proven to be a 
                    likeable boss that can, together with his management team, 
                    deliver change and produce astonishing turnaround results.<br>
                    <br>
                    <b>Life's journey<br>
                    </b>Al Hooti got his break in the late 1970s at Prospection 
                    Limited, a Canadian mining company doing geological and 
                    geophysical exploration for copper and gold in Oman's 
                    northern ophiolite belt. He joined them as a trainee working 
                    in Sohar soon after finishing secondary schooling in Qatar.
                    <br>
                    <br>
                    &quot;Before joining the company, I was thinking of doing 
                    petroleum geology. My father was adamant that I should 
                    choose a better career. After a year of successful training 
                    in geology in Sohar, I went to Canada. He was proud of me 
                    for making my own mind up and the success I had achieved,&quot; 
                    he says. <br>
                    <br>
                    Prospection sponsored Al Hooti's studies which involved a 
                    pre-university year in Newmarket, Ontario followed by a 
                    degree at Colorado University in Geology and Mathematics. 
                    After his graduation, he rejoined the company (which 
                    transformed into Oman Mining Company) and conducted tasks 
                    such as geological mapping, geophysical surveys, and 
                    sampling. He also took responsibility of geological works in 
                    Sohar's Bayda Mine until its copper reserves were depleted.
                    <br>
                    <br>
                    In 1985, he chose to move to the Ministry of Commerce and 
                    Industry (MCI) despite being offered a place at Sultan 
                    Qaboos University to continue his geological studies. He 
                    feared that the depletion of copper mines would make his 
                    prospects at OMC less favourable.<br>
                    <br>
                    &quot;After joining the Ministry I studied two diplomas. One was 
                    based on the subject of Consultancy Services for Small 
                    Industries at Nagoya in Japan. The other I undertook at 
                    Maastricht Institute of Management in the Netherlands on the 
                    Project Management Cycle.&quot; The courses made it easier for 
                    him to craft his business skills. <br>
                    <br>
                    Al Hooti worked his way up to the post of Assistant 
                    Director-General of Industry before being approached by his 
                    superiors to head the management at Oman Cement Company in 
                    1992. At this time, the MCI had responsibility for OCC. 
                    Needless to say, he accepted the challenge although it took 
                    a month for him to wrestle with the pros and cons of such a 
                    decision. The rest is history. <br>
                    <br>
                    <b>A geological passion</b><br>
                    Al Hooti's love of rocks began at a very early age. He loved 
                    to wonder around the mountainside hunting for fossils or 
                    unusual stones like they were playgrounds. <br>
                    <br>
                    &quot;Many people say Oman is a heavenly museum for geologists,&quot; 
                    he says. &quot;There is little need to dig deep underground to 
                    find the stones or rock formations that you are looking for 
                    as they can be found mostly on the surface. Geologists here 
                    are very lucky.&quot; <br>
                    <br>
                    He remembers vividly a seminar conducted by one university 
                    lecturer which involved some research on NASA (National 
                    Aeronautics and Space Administration), the US space 
                    exploration agency. &quot;The lecturer brought some stones from 
                    the moon and then talked at great length about them. He 
                    literally expanded the universe of possibility in the minds 
                    of students.&quot; <br>
                    <br>
                    On NASA's roving missions to Mars, Al Hooti adds: &quot;There has 
                    been much debate as to whether it is worth spending so much 
                    money going to Mars. I feel someone has to do it and we are 
                    lucky that the US and Europe are directing so many resources 
                    to space exploration. <br>
                    <br>
                    &quot;As for mining on the moon, I would remind you that in the 
                    past, people found it difficult to believe that platinum 
                    would become more valued than gold. My hunch is that mankind 
                    could find something more precious then diamonds and more 
                    useful than silicone on the moon.&quot; Undoubtedly, this is 
                    something that NASA's scientists are already banking on 
                    having recently outlined plans to set up a temporary base on 
                    the lunar surface to help progress the possibility of 
                    sending manned missions to other planets like Mars.<br>
                    <br>
                    <b>Entrepreneurial spirit </b><br>
                    Back down to earth, Al Hooti recounts that in the 1980s he 
                    joined up with three other graduate friends decided to start 
                    a business. &quot;There was a good mix of expertise,&quot; he says. 
                    &quot;One was an industrial engineer, a computer expert, the 
                    third a civil engineer and I added the geological edge,&quot; he 
                    says. The group were confident of success. Perhaps 
                    over-confident. <br>
                    <br>
                    &quot;Each of us seemed to drift towards advancing our individual 
                    careers rather than to move the business forward as we had 
                    all originally intended. Finally, we came to a mutual 
                    agreement to dissolve the company.&quot; <br>
                    <br>
                    The experience didn't faze Al Hooti. With an impressive 
                    stroke of ingenuity and demonstrating a deft touch of 
                    fortitude, he turned his passion for old stones into a 
                    lucrative hobby. He explains that in 1990, he came up with 
                    the idea of selling small rocks and minerals of Oman as 
                    trinkets to tourists. In his spare time, he travelled to 
                    different locations collecting minerals and stones such as 
                    gypsum, geode, calcite and sulphide. He chipped away at 
                    these at his home to make smaller stones, designed some 
                    packaging to go with them, glued the stones in a 
                    presentation case containing printed information on the 
                    country's geological history and set about marketing the 
                    souvenirs to bookshops in the region. His hard work paid 
                    off. He sold a few thousand of the booklets entitled Geology 
                    in the Sultanate of Oman - Small Stones from a Great 
                    Country. And he says that the venture earned him enough to 
                    pay for a Mitsubishi Pajero. <br>
                    <br>
                    &quot;I would welcome the idea of someone else selling small 
                    rocks to visitors. Life is too hectic for me to continue 
                    producing such souvenirs,&quot; he says. <br>
                    <br>
