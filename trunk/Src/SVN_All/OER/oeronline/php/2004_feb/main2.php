                  momentum?</b> <br>
                  I am hoping that there will be more such forums in Oman so 
                  that we can really focus on having an honest, straightforward 
                  dialogue between the industry and the regulators so that 
                  issues that are hindering the industry's growth come to the 
                  fore and can be addressed. <br>
                  Given the inherent financial skills and intelligence levels of 
                  those who attended the conference, I have challenged them to 
                  take specific things from the seminar and implement them. And 
                  I am convinced that at least each of these six private 
                  companies will do something to increase their share. <br>
                  As far as the other part of the audience is concerned 
                  (regulators), I don't know if it will be [possible for them to 
