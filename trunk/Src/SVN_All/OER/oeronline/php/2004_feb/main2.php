                  workplace because they are not in the commercial side, but I 
                  am sure that their awareness would have definitely increased. 
                  I saw nothing but enthusiasm and I am hopeful that we will see 
                  certain steps from the regulators in the intermediate term.
                  <br>
                  <br>
                  <b>Where do you think Omani leasing &amp; finance industry stacks 
                  up against its peers in the GCC? </b><br>
                  Corporate leasing here in Oman as a percentage of GDP is 
                  3/10th of 1 per cent. Now, in the entire world, the average is 
                  2 per cent, including the mature and the emerging markets etc. 
                  So, that's the good news for Oman. I.e., the industry has room 
                  to grow based on that one very common global average. <br>
                  Oman's history is longer - retail corporate size is about the 
                  same as the rest of the countries in the region. More or less, 
                  all of them are at par with respect to product offering and 
                  level of sophistication. The expat contribution here will make 
                  the difference.<br>
                  <br>
                  <b>How long do you think it will take Oman's leasing industry 
                  to attain maturity? </b><br>
                  I presented something called the 6 stages of the leasing 
                  cycle. In the US, it took us 40-50 years to get from Stage I 
                  to Stage VI. I will use an intermediate case in point - in 
                  Australia, it took them 20 years to get from Stage I to Stage 
                  VI for obvious reasons. The technology cycle in leasing got 
                  shorter because these guys studied the evolution of the US 
                  industry.<br>
                  <br>
                  Using the US and Australia as the two cases in point, this day 
                  onward, Oman has the same opportunity. It can progress down 
                  that leasing cycle much faster. And that template actually was 
                  presented in the seminar. Oman is already at the end of Stage 
                  I and we really are entering Stage II. I believe Oman can 
                  enter Stage V in about 5 years. Stage VI is maturity. Stage V 
                  gets us at least into securitisation (a transfer of lease 
                  receivables to investors through a bankruptcy remote special 
                  purpose entity). Stage III is operating leases. <br>
                  <br>
