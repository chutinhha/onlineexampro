                  lending by banks? </b><br>
                  Loans or credit, obviously, have to do with one thing and one 
                  thing only - the creditworthiness of the customer. You do the 
                  credit due diligence and that's it. In the context of 
                  asset-based finance, you've got an underlying asset which, in 
                  the corporate world, if it's a productive asset, will generate 
                  the cash to pay back the rentals. The underlying asset is 
                  creating the incremental cash to pay back the rental. So, 
                  that's really what asset-based lending is.<br>
                  Leasing, in the past 20 years, has grown at a Compound Annual 
                  Growth of 12 per cent whereas banking's growth in terms of 
                  credit has been in single digit. So, leasing has been popular 
