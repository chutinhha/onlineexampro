                  <font face="Verdana" size="1"><b>Welcome to Oman, Mr Amembal. 
                  How did the two-and-a-half-day seminar-cum-workshop on Asset 
                  Financing &amp; Leasing go, and what was the quality of attendees?
                  </b><br>
                  Thank you. Two-and-a-half days in the classroom have given me 
                  a lot of perspective on Omani finance companies. I listened 
                  very carefully to the audience, their questions, their 
                  comments... The quality has to be prefaced - one, it is a 
                  young industry. Notwithstanding the fact that leasing has been 
                  around here for about 16-17 years, from everything I'm told, 
                  it's the last five years that have really created the growth 
                  mode. So, I'm going to call it a 5-to-7-year-old industry.<br>
                  <br>
                  In that context, the quality is impressive even though a large 
                  part of the business is a simple business, i.e., the retail 
                  automotive business, which doesn't require a genius to 
                  operate. Nonetheless, finance companies in Oman are staffed by 
                  intelligent, smart, savvy people. <br>
                  <br>
                  (During the seminar) I was discussing things that I discuss in 
                  reasonably mature markets because the objective was to take 
                  the audience to the next level. You don't come here and tell 
                  them what they already know or the basics. So, I was truly 
                  impressed, particularly with the personnel from the finance 
                  companies, who were keeping me alert. Two-thirds of the 
                  attendees were expatriates while the rest were Omanis - it was 
                  a homogenous mix of intelligence. <br>
                  <br>
                  Even the personnel from the Central Bank of Oman (CBO) -- and 
                  when I say 'even from', I mean that when you go into the 
                  regulatory environment, you usually encounter a different 
                  person -- kept me awake all the time. <br>
                  <br>
                  <b>Broadly speaking, what were the issues that were discussed 
                  during the seminar? <br>
                  </b>We discussed a lot of issues - all the way from policy to 
                  hard roll-up-your-sleeve mechanics, e.g., how to convince a 
                  customer to lease because leasing is cheaper than cash, etc. 
                  Really, almost A to Z. <b><br>
                  <br>
                  What, according to you, were the benefits for those who 
                  attended the seminar?<br>
                  </b>This was the first seminar of its kind not only in Oman 
                  but also in the region. So, the audience here never had the 
                  opportunity to sit through dedicated lease education before 
                  this seminar. So, obviously, it opened their eyes. After all, 
                  they saw the potential the product has. <br>
                  Given the reasonable infancy of the industry (e.g., I went 
                  through 27 benefits that leasing can offer; and I don't 
                  believe that all of those present were aware of all of them), 
                  it did open their eyes. For instance, off-balance-sheet 
                  finance is one benefit that, maybe, they've heard of, maybe 
                  they've understood as well, but not in detail and definitely 
                  not in practice. <br>
                  Off-balance-sheet financing itself has certain benefits such 
                  as the enhancement of ratios, improvement in earnings etc. 
                  which they may not have been aware of in depth prior to this 
                  seminar. <br>
                  What this seminar and others like it will do is that it will 
                  help the attendees increase their penetration. Otherwise, why 
                  would a lessor invest his time and money in such a forum 
                  unless he went back and did more business? I challenged them 
                  at the end of two-and-a-half days and said each of you has to 
                  give your CEO three ideas that are implementable. I'm hoping 
                  that they will do that. <br>
