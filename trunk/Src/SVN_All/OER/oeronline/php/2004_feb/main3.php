            <font face="Verdana" size="4" color="#FF0000">Performance pays</font><p align="justify">
            <b><font face="Verdana" size="1">Fixing appropriate top executive 
            compensation can be a challenging task for the board. Afshan Patel 
            takes a look at what goes into determining top management salary 
            structures and what components should be included to align it to the 
            long-term interests of the shareholders</font></b></p>
                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="im3.jpg" align="left" hspace="5" width="200" height="181">The 
                    realm of top executive compensation is like a maze. There is 
                    little guidance available to help design an appropriate 
                    compensation package for top management. Reading about some 
                    of the top executive payouts in the Western world makes you 
                    flip over to the cover to find out whether it is actually a 
                    business magazine you are reading or a fairy tale. 
                    Undoubtedly, some of the thinking that goes into designing 
                    an exceptional compensation package could be as imaginative 
                    as writing a fairy tale. Lawrence J. Ellison, chairman and 
                    CEO of Oracle Corporation, made $706 million (RO272 million) 
                    in 2002 - almost all of it from long-term compensation. <br>
                    <br>
                    Jozef Straus, co-chairman, president and CEO of JDS Uniphase 
                    made $151 million (RO58 million), out of which just half a 
                    million was in the form of salary and bonus, the balance 
                    being long-term compensation. The executive vice-president 
                    and COO of JDS Uniphase made $121 million (RO47 million), 
                    all in salary and bonus and nothing from long-term 
                    compensation. The higher up in the organisational structure 
                    that the appointment has to be made, the more complicated 
                    becomes the issue of fixing an appropriate pay package. 
                    Fixing appropriate top executive compensation can be a 
                    challenging task for the board. Their sensitive handling of 
                    the issue can make the difference between a productive, 
                    motivated task force or an unproductive, apathetic or even 
                    worse, a destructive workforce.<br>
                    <br>
                    Top management is in a cadre of its own. This management 
                    echelon comprises the decision-makers in an organisation. 
                    They are privy to a lot of knowledge about the organisation, 
                    the industry and the impact of various internal and external 
                    factors on the organisation. Top executives or top 
                    management of commercial organisations play a very important 
                    role in any economy. To state a few, they facilitate the 
                    creation of wealth, they create employment and growth 
                    opportunities, and they generate revenue to the government 
                    by way of taxes. The more dynamic the top management is, the 
                    greater are the chances of growth in the organisation. <br>
                    <br>
                    There has been a lot written about compensation packages 
                    paid out to various top managers and more gets written when 
                    a deal which involved big bonuses goes bad or companies 
                    themselves go bankrupt. Suddenly, top management starts 
                    looking like villains robbing the shareholders of their 
                    wealth. While there have been innumerable cases where greed 
                    has made management overlook the interests of the 
                    shareholder, whose interest they are meant to protect, this 
                    is not the rule. <br>
                    <br>
                    Top management is one of the limited resources in the wealth 
                    creation cycle and an important one at that. It is not 
                    uncommon for the top executive to make 500 times or even 
                    more than that of the average employee of the organisation. 
                    Why does this happen, and is it fair are questions often 
                    raised. Whether fair or not is debatable and at different 
                    stages of a career, one's views on the fairness of this may 
                    change. Initially, these differences may appear grossly 
                    unjust. As one moves higher up the organisational hierarchy, 
                    the differences may start appearing more and more justified. 
                    If one can make it to the top, the difference appears really 
                    small and a case may be made for bigger differences. This is 
                    not the story of the grapes being sour. Rather, if one is 
                    able to rise above the rest, then s/he also understands what 
                    it takes to get there. <br>
                    <br>
                    What, indeed, does it take to get there? Leadership 
                    qualities, ability and confidence to take calculated risks, 
                    technical knowledge, foresight and the ability to visualise 
                    the future, strategic planning skills, ability to balance a 
                    number of conflicting factors, ability to always keep the 
                    'Big Picture' in mind, astute business sense, understanding 
                    of the world economy, local markets and the interaction of 
                    these forces and an ability to keep calm in a storm. <br>
                    <br>
                    Given this complex role of top management, the issue of 
                    designing suitable compensation packages for them also 
                    becomes complex. What are the factors that make designing 
                    suitable executive compensation packages complicated? <br>
                    <br>
                    1. In public limited companies, there is segregation between 
                    ownership and management. This prevents owners from closely 
                    interacting with the management. If owners were closely 
                    interacting with management, like they would do in a 
                    proprietary concern, they would be better positioned to 
                    evaluate the efforts that go into running the organisation 
                    and this would make it easier for them to arrive at an 
                    equitable compensation package. <br>
                    <br>
                    2. Owners generally do not have the same feel for the 
                    business and familiarity with the business that the 
                    management has. Owners come from diverse backgrounds, some 
                    related to the field in which the organisation runs, some 
                    unrelated. Usually, they lack the understanding of the 
                    complexities that are involved in running the organisation. 
                    Often, the board members bring the organisation into focus 
                    just before or at a board meeting. Therefore, the board 
                    members find themselves inadequately prepared to offer an 
                    adequate compensation to the top management. In contrast, a 
                    committed and involved top management is well aware of what 
                    the contribution of other employees is and, therefore, can 
                    offer relatively more equitable compensations to the rest of 
                    the staff. <br>
                    <br>
                    3. Decisions sometimes impact a business long after they 
                    have been taken, making it difficult to determine the cause 
                    and effect of the decision within the same time period. An 
                    improper business decision taken today may adversely affect 
                    the organisation after 3 or 4 years. This is more likely in 
                    the banking sector or in sectors with longer business 
                    cycles. In the banking sector, loans given today may seem to 
                    be profitable propositions. But if these deteriorate and 
                    become non-performing assets, it takes a couple of years to 
                    actually slot them as bad and to book a loss on them. In 
                    such cases, current year's or previous years' performance 
                    may not provide a suitable foundation to fix compensation. 
                    Where top management has stayed long enough with a company, 
                    for around 8 years or so, there is enough evidence of past 
                    performance to back the design of an appropriate 
                    compensation package. <br>
                    <br>
                    4. Impacts on business are not always the result of action 
                    by management. Sometimes, external factors beyond 
                    management's control may impact business. This can be 
                    favourable or unfavourable to top management or owners. 
                    E.g., a strengthening of a domestic currency vis-à-vis the 
                    currency of a country from which imports are made, will make 
                    the imports cheaper. If the selling prices are not changed, 
                    this will get reflected as higher profits to the 
                    organisation. These higher profits are attributable to 
                    market forces operating in the currency markets rather than 
                    the direct efforts of the management. The reverse could also 
                    happen. Despite the best efforts of management to produce 
                    good results, a weakening of a domestic currency could make 
                    imports expensive. If all the cost increase cannot be passed 
                    on to the customer, there will be a decline in the 
                    performance of the organisation - all other factors 
                    remaining constant. The first situation should not result in 
                    higher management compensation while the second should not 
                    result in lower compensation. However, such factors do 
                    affect compensation packages. <br>
                    <br>
                    Despite the difficulties that present themselves in 
                    designing compensation packages for top management, this is 
                    a task that has to be done. This is also a task that 
                    shareholders/ boards should develop skills for and 
                    constantly fine-tune those skills. What are the different 
                    types of compensation packages that are generally offered to 
                    top management and what is the impact of the various 
                    components on decision-making?<br>
                    <br>
                    <b>Types of Compensation packages:<br>
                    Fixed Salary</b><br>
                    A fixed salary, irrespective of the performance of the 
                    company, is generally given to new incumbents, even at the 
                    top management level. Maybe it's the owners' way of finding 
                    out how well the new incumbent is going to handle his 
                    responsibilities. This is also a way of ensuring that the 
                    new incumbent does not get undeservedly benefited or 
                    penalised for the actions of his predecessor. However, a 
                    fixed salary does nothing to motivate top management into 
                    spurring the organisation into growth mode or challenging 
                    itself to keep improving performance. In management jargon, 
                    such a situation would be said to be one in which the 
                    interests of the employee and the owners are not aligned. 
                    While the owners' objective is to maximise profits, 
                    irrespective of whether the top management does this or not, 
                    they are entitled to a fixed salary. Therefore, they would 
                    not be motivated to take a little extra risk that may bring 
                    in extra profits. They would be interested in taking the 
                    path of least risk. <br>
                    <br>
                    <b>Performance-based component </b><br>
                    Such compensation packages have a fixed base salary and a 
                    performance-based component of a bonus or incentive. The 
                    performance-based component may be a percentage of net 
                    profits, a number of months' salary or determined in some 
                    other manner based on the performance of the employee or the 
                    organisation. The incentive component may be fixed at the 
                    discretion of the board every year or the basis may be 
                    pre-determined by the employment contract.<br>
                    <br>
                    The advantage of including a performance-based component is 
                    that it aligns the interests of the top management with that 
                    of the owners. Both the owners and the management now work 
                    with the objective of profit maximisation. However, such a 
                    compensation package is not without its drawbacks. Such a 
                    compensation package suffers from the ill effects of 
                    'differences in decision-making horizon'. <br>
                    <br>
                    <b>Long-term compensation component </b><br>
                    Including a long-term component in a compensation package 
                    redresses the ills of 'differences in decision-making 
                    horizons' between the owners and the top management. 
                    Decision-making horizon refers to the period over which the 
                    decision makers are concerned with the outcome of their 
                    decisions. As owners are interested in the perpetual 
                    continuity of their businesses, they have an unlimited 
                    decision-making horizon. Top management may not always have 
                    a long-term decision-making horizon. Therefore, when they 
                    don't, there is again a situation of misaligned interest. 
                    Top management may have a decision-making horizon of about 3 
                    years or 5 years or whatever suits their interests. They may 
                    then make their decisions so as to maximise profits and 
                    thereby their bonuses during their decision-making horizon, 
                    whatever the impact of such decisions may be after they 
                    leave the organisation. <br>
                    <br>
                    In the banking sector, inappropriate loans given to boost a 
                    balance-sheet during the tenure of a particular top 
                    management may result in spectacular profits and bonuses. 
                    However, those loans may have to be written off at a later 
                    date, a date that falls outside the decision-making horizon 
                    of the top management, although within the decision-making 
                    horizon of the owners who eventually bear the loss of 
                    writing off those loans. <br>
                    <br>
                    To take an example from outside the banking sector, a 
                    manufacturing company may have the option to choose from two 
                    machinery items. Machinery A boosts production during the 
                    first 5 years but is subject to significant repair costs 
                    after that. Over a 10-year period, the overall profitability 
                    of the Machinery B is higher. In other words, the wealth of 
                    the company and, therefore, the wealth of the shareholders 
                    would be higher after 10 years if it were to install 
                    Machinery B than it would be with Machinery A. This is 
                    because Machinery B does not have the significant repair 
                    charges after year 5. Top management that has a 4 to 5 year 
                    decision-making horizon would install Machinery A, which 
                    boosts production during the first 5 years irrespective of 
                    the impact on shareholders' wealth at the end of the 10-year 
                    period. The initial 5-year period is what is going to 
                    determine the top management's bonuses. <br>
                    <br>
                    <img border="0" src="im4.jpg" align="right" width="150" height="203">To 
                    align shareholders' and top management's long-term 
                    interests, a long-term component is included in compensation 
                    packages. This generally takes the form of 'stock options' 
                    in the Western world. Stock options give employees an option 
                    to buy shares in the company they work for, at a fixed 
                    price, generally the market price existing at the time of 
                    granting the option. The option is to be exercised after a 
                    set future date. The logic behind granting stock options is 
                    that the employees will work for the increase in the overall 
                    value of the company, which will get reflected in the 
                    increase in share prices. At the future date when the 
                    employee can exercise his option, he benefits by buying a 
                    high priced share at a low price that was previously fixed 
                    by the terms of his stock option. Every year top management 
                    gets stock options as part of the compensation package and 
                    this is expected to align the decision-making horizon of the 
                    shareholders and top management. <br>
                    <br>
                    In Oman, stock options are not prevalent. In the West too, 
                    stock options are not without complications and the 
                    authorities are still trying to answer many questions 
                    regarding their accounting treatment and their use as a tool 
                    to align the long-term objectives of the shareholders and 
                    the top management. This tool has sometimes been changed to 
                    suit the interests of top management in a way that the 
                    original objective is lost. If a company does not do well 
                    and the share price, instead of rising, falls to a price 
                    lower than the price at which the option was granted, there 
                    will be no advantage to top management from this part of 
                    their compensation package. <br>
                    <br>
                    Boards have sometimes resorted to bringing down the original 
                    grant price to ensure that stock option holders gain from 
                    their stock options. What happens to the shareholders? They 
                    have to still bear the losses from the reduction in their 
                    wealth resulting from lower share prices. Thus, while stock 
                    option holders benefit, shareholders will not, if such 
                    action is taken. Therefore, stock options are a good tool to 
                    use to align the interests of top management and 
                    shareholders, provided it is used as intended. Maybe as the 
                    Omani markets mature, the authorities may consider allowing 
                    stock options. <br>
                    <br>
                    Therefore, a comprehensive top management compensation 
                    package would include a fixed base salary, a performance 
                    based incentive component to align the interests of top 
                    management and shareholders and a long-term incentive 
                    component to align the decision-making horizons of top 
                    management and shareholders. The author is a Chartered 
                    Accountant, a Certified General Accountant (Canada) and a 
                    Certified Internal Auditor and has worked with a large 
                    Public Accounting Firm, Central Bank of Oman and with the 
                    University of Toronto, Canada</font></p>
