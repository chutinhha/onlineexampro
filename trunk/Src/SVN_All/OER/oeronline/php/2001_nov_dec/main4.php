                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Over a Barrel</font><p><font face="Verdana" size="1"><b>September 11 could make or break foreign oil investment in the Middle East, says Chris Ross, vice-president of global energy at US management consultancy Arthur D Little&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2001_nov_dec/im8.jpg" align="left" hspace="5" vspace="5" width="206" height="314"></font><font face="Verdana" size="2">The global energy industry is still debating the impact of September 11. Subsequent events - US air strikes on Afghanistan, global economic slowdown, the Anthrax bio-terrorism attacks - have created a mood of such uncertainty that no one can predict the precise implications.<br>
                    However, now that the dust has settled and the industry has had time to reflect, some trends are emerging.&nbsp;<br>
                    "You have to look at different time frames," says Chris Ross, vice-president of global energy at US management consultancy Arthur D. Little.<br>
                    "The first implication, over the next 12 months, is the probability of economic slowdown, lower oil demand, greater pressure on OPEC and the probability of lower prices. That is something that companies and governments will have to take account of."<br>
                    In the immediate aftermath of the World Trade Centre attacks, oil prices rose sharply, to more than US$30 per barrel, as traders feared a widespread Middle East war and consequent supply disruption. As it became clear that this was unlikely, the main fear became global economic slowdown, and the negative effects that would have on oil demand. As a result, prices fell towards $20 a barrel.<br>
                    Most analysts agree that prices are unlikely to rebound over the next couple of years. But establishing the wider implications requires a little more subtlety. "In the longer term, you can break it down into a number of different scenarios," says Ross.<br>
                    In the Middle East, the big issue is foreign investment. Since the late 1990s, a host of big regional oil producers have been wooing energy majors in a wave of privatisation. The likes of ExxonMobil, BP, Royal Dutch/Shell and TotalFinaElf have shown great interest. The events of September 11 could significantly help or hinder that process.<br>
                    "The hope of international oil companies was that slowly but surely Middle East countries would open up to foreign investment," says Ross.&nbsp;<br>
                    "Saudi Arabia was seen to be pushing open the door, which is why they all piled into those gas projects. The hope was that over time, the US and Iran would reconcile. Now, following September 11, I'm not sure if that is more or less likely. There are two possible scenarios.<br>
                    "Scenario one is that the US engages the international community, including moderate Islamic nations, in a constructive joint-effort to address problems. In this case, things get better. Scenario two is that this proves to be impossible and things get worse."<br>
                    There are some grounds for optimism. In the days that followed the September 11 attacks, President Khateimi in Iran and Colonel Gadhaffi in Libya sent strong messages of support to the US. A month earlier the United States Congress had renewed five-year sanctions against both countries for alleged "state-sponsorship" of terrorism. It is now hoped that their moderate stance will smooth the way for sanctions to be lifted - especially as President Bush argued against the five-year extension.&nbsp;<br>
                    But such an outcome is by no means guaranteed.&nbsp;<br>
                    "It has certainly changed the risk profile of the region, but people are aware that this could still go either way," stresses Ross. "There is a possibility that this is a cathartic event that will effect a better, richer dialogue between the West and Islam that will result in people trying to understand each other better. This would ultimately help the Middle East join the international economic community in full, rather than just as a resource provider. Or it could go very badly wrong."<br>
                    Outside the region, of crucial importance is the impact September 11 will have on energy policy in the US - the world's biggest consumer that is responsible for around one quarter of global energy use. "It will put pressure on the US administration and Congress to find a compromise on energy policy," says Ross.&nbsp;<br>
                    President George W. Bush is regarded as an "oil man" - he was Governor of Texas, capital of America's oil industry. As such, one of his first moves as President was to draw up a new energy plan, to cut America's heavy dependence on imported oil (ironically, the US is the biggest buyer of Iraqi crude).&nbsp;<br>
                    The plans had two elements. The first was to call for greater exploration and production in areas such as Alaska, which outraged environmentalists. The second was to find ways of boosting energy conservation. The policy met with much resistance from green lobbyists, but that resistance has been weakened by the presence of more pressing concerns.<br>
                    "You are likely to see an energy policy that puts more money and effort behind conservation, energy efficiency and renewable energy," says Ross. "At the same time, you will see the opening up of new Federal lands to exploration. The US will adopt policies that reduce its level of imports. This deal has been difficult to pull off for the Bush-Cheney administration so far. Now it is more likely that will happen."<br>
                    If the US can succeed in slashing its import demand, it will have implications for the entire industry. But it is unclear how strong America's resolve will be if and when the attacks fade from memory.<br>
                    September 11 was hugely significant, but it must be acknowledged that it did not change everything. Downward pressure on oil prices had been building for months - in many ways, September 11 was no more than the catalyst for a price correction that would have happened anyway.<br>
                    "There was always going to be pressure on oil prices in 2003 and 2004," says Ross. "(The oil industry) is a self-regulating system. If prices go down, people will stop drilling in high cash-cost areas such as the Gulf of Mexico, on-land US and parts of the North Sea. That was always going to happen in 2003 - this has just brought it into 2002."<br>
                    So a price fall could have a silver lining for OPEC, particularly Middle East members who enjoy some of the lowest production costs in the world (as low as $1 per barrel compared with as much as $15 per barrel in high cash-cost areas). OPEC has been losing market share to higher cost rivals, but this trend will slow - or even reverse - if prices are low.<br>
                    "At any time, the Middle East has a comparative advantage - it is a case of whether it wishes to attract investment. September 11 hasn't changed any of that. What it has done is change the price cycle a bit. It has brought the price cycle forward," Ross says.<br>
                    For all these reasons, oil companies are highly unlikely to abandon the region unless it is engulfed in full-scale war - and even the most pessimistic observers think that is unlikely. "The Middle East is still where the oil is," says Ross. "So oil companies say to themselves 'we are in the oil business, we had better be there'."&nbsp;</font></p>

