                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Muscat City Centre</font><p><font face="Verdana" size="1"><b>Will Muscat City Centre live up to expectations?&nbsp;<br>
                    Kavita Pandit asked General Manager Robert Fusco what all the fuss is about</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2001_nov_dec/im1.jpg" align="left" hspace="5" vspace="5" width="400" height="92">October witnessed the opening of the much-awaited Muscat City Centre, the first 'super-mall' in Oman. Covering 44,000 square meters, and with 1,500 parking spaces, the RO25 million mall promises to take shopping to a new level in Muscat.<br>
                    It features a Carrefour hypermarket - another first for the Sultanate - a food court, entertainment, and a vast array of shops.<br>
                    In the first week of trading, City Centre received 200,000 visitors, even though less than a third of its 80-odd potential outlets were open.&nbsp;<br>
                    General Manager Robert Fusco says the plan is to unveil the mall gradually over three months in a "tease and reveal campaign".<br>
                    "We don't want to unwrap all our presents at the same time," he says. "This way, the present-giving lasts longer and the delight lasts longer."<br>
                    By November 1, 29 outlets were open, and by January 2002, 90 per cent of the mall will be operational.<br>
                    Dismissing suggestions that the super mall will muscle local Muscat retailers out of business, Mr Fusco says that on the contrary, Muscat City Centre will stop Omani shoppers going to Dubai to spend their money, which, he argues, will benefit all Muscat shop owners.&nbsp;<br>
                    "I want to work with the existing businesses rather than against them. For each one of us to get a share of the pie, we need to make the business cake bigger. This can be achieved if we stop more and more people from going to Dubai."</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_nov_dec/im2.jpg" align="left" hspace="5" vspace="5" width="200" height="162">Mr Fusco says the two main reasons people go to Dubai to shop are choice and price.&nbsp;<br>
                    "The third is entertainment. At City Centre, we definitely provide the choice. Carrefour is cheaper than anywhere in Oman. On an average, it is five to 10 per cent cheaper, not on individual products but on an average basket," he says.<br>
                    Carrefour is Europe's largest hypermarket chain, with 700 hypermarkets in more than 26 countries. It is the biggest supermarket in Oman by far, with over 60,000 items on sale in 8,000 square meters of shopping area. It has 32 cash counters and 400 staff.<br>
                    On the entertainment front at City Centre, a major attraction for children will be the Magic Planet, due to open in November.&nbsp;<br>
                    It will include a large selection of video games, theme rides, remote-controlled cars and buggies, a full-size carousel, bump-a-cars, a train ride and many more fun games.&nbsp;<br>
                    In addition, children will be entertained by cartoon characters, jugglers and clowns.&nbsp;<br>
                    Mr Fusco says the Centre has created 1,500 jobs and has already achieved 45 per cent Omanisation. The security staff is 100 per cent Omani.&nbsp;<br>
                    Though it is early to talk of expansion plans, Mr Fusco says there is additional land available for "Phase II"&nbsp;<br>
                    "Currently, our main concern is to establish Phase I. We have not made any decisions how to develop Phase II yet."&nbsp;<br>
                    Confident of success in Muscat, Mr Fusco says: "a shopping centre is a destination where one should be able to meet all their shopping needs under one roof, and that is exactly what we are looking to provide."</font></p>

