                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Mideast violence unsettles oil market</font><p align="center"><img border="0" src="../../images/2001_january_February/img4.jpg" width="300" height="244"></p>
                    <p><font face="Verdana" size="2">Markets hate nothing more than uncertainty. It's bad enough when that uncertainty revolves around economic factors such as interest rates, inflation or money supply. But when bloodshed is involved, it takes on a whole new dimension. The recent upsurge in hostilities between Palestine and Israel caused oil markets to take particular fright.&nbsp;<br>
                    <br>
                    "The situation in the Middle East is one of the main factors keeping oil prices high," one well-placed oil industry analyst told OER. "Prices remained strong and volatile in October, rising and falling in line with developments in the Middle East," said the Paris-based International Energy Agency (IEA) in its November monthly report.<br>
                    <br>
                    Specifically, traders are worried that the on-going conflict will spread outside the borders of Palestine and Israel, and thereby affect global crude supply. If military activity spills over into neighbouring countries, physical supply could be disrupted.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2001_january_February/img5.jpg" align="left" hspace="5" vspace="5" width="250" height="172">However, the markets take extremely seriously the possibility that Palestine's Arab neighbours could shut off oil supplies in show of support. Gulf Arab producers have been outspoken critics of Israel and, perhaps more importantly, some Western powers such as the US have been unwavering in the public support of Israel. A supply shut down would send markets, and indeed the global economy, into chaos.<br>
                    <br>
                    Certainly, Arab and many international governments have been incensed by the recent violence in Palestine, which has left more than 346 people dead - 291 of them Palestinians. Saudi Arabia's Crown Prince Abdullah, who runs the country on a day-to-day basis, has been particularly outspoken.<br>
                    <br>
                    "We know who is helping them and who is giving them support in the United States and Europe. But such help will not last forever even if it goes on for 10, 20, 50 years, or even a century. The Arabs surround them [Israel] from all sides, and thanks to God they are growing stronger. Al-Quds (Jerusalem) is the most important issue for us. Even if we have to sacrifice our children... we will never surrender, never," he vowed.<br>
                    <br>
                    Crown Prince Abdullah's has not been a lone voice. "Arab countries should conform to and take seriously the resolutions of the extraordinary Arab summit," said Oman Minister of State for Foreign Affairs Yussef bin Alawi bin Abdullah, a reference the Arab League meeting in Cairo held on October 21-22. "The latest Israeli escalation and the bombardment of Palestinian Authority premises have created a dangerous, difficult and delicate situation."&nbsp;<br>
                    <br>
                    On October 12, the Sultanate of Oman closed its trade office in Tel Aviv and that of Israel in Muscat, both opened four years ago - as a gesture of support for the Palestinian cause. A month later Qatar closed the Israeli trade mission in Doha after Saudi Arabia threatened to boycott the Organisation of Islamic Conference, held in Doha in November, if the office remained open.<br>
                    <br>
                    Oil markets have almost become used to occasional strong words from Arab leaders, so such comments, in isolation, would not cause undue concern. What worries traders is that those words could be translated into firm actions - particularly co-ordinated action. That is precisely what happened.<br>
                    <br>
                    Shortly after trouble flared, regional leaders met in Cairo for an extraordinary Arab League summit to discuss their reaction. As well as making the expected condemnations of Israel, they pledged to establish a $US1 billion fund to back the Palestinian struggle. Many traders worry that if current moves do not have the desired effect within the next few months, a disruption of oil supply may be the next step.&nbsp;<br>
                    <br>
                    Esmat Abdel-Meguid, secretary-general of the Arab League, warned the global community that the Arab world is an important strategic region with valuable resources. "There is anger in the Arab world and the Islamic world... It would be a very serious miscalculation to think that this can be taken lightly and that it will (go away). This should be a message to Israel from the Arab countries, from the Muslim countries, that this situation cannot continue in Israel... They think they can control the area. They are committing a very big mistake."<br>
                    <br>
                    Against this background, crude oil prices remained above $US28 per barrel throughout October and November, despite four Opec output hikes in 2000. And although crude prices dipped slightly in early December, some seasoned observers cited historical evidence that the implications for oil markets of such regional unrest can be far more long term.<br>
                    <br>
                    "The price hikes of 1970s and 1980s were caused principally by external political events rather than by policy design on the part of Opec," said Dr Fadhil Chalabai, executive director of London's Centre for Global Energy Studies, during a speech in Beirut in October. "The first oil-price shock occurred when Opec's Arab oil-producers decided to use oil as a political instrument with which to exert [pressure] on the US and the West during the Arab-Israeli War of 1973. This caused prices to soar in world markets which, for Opec, became a new yardstick with which to fix its price at a higher level. The second price shock was caused by the interruption of Iran's oil exports in 1979, as a result of the Ayatullah Ruhollah Khomeini's revolution. Oil prices spiralled even higher following Iraq's invasion of Kuwait in 1990."<br>
                    <br>
                    The Israeli-Palestinian situation has clearly been an important factor in oil's recent rally, but it is not the only one. The $30 per barrel prices seen in recent months are blamed by most analysts on a combination of issues. Opinions differ on the relative importance of these factors, particularly between producers and consumers. But all accept that a host of influences are at work.<br>
                    <br>
                    Of these, low global stocks are the most important factor. Estimates vary about exactly how much supply the world has to fall back on. But virtually everyone aggress it is significantly less than this time last year and lower than what would be considered normal. "There's no real buffer zone," one analyst told OER. "If there is a supply disruption, either from Opec's Arab members or from Iraq, where will the shortfall be made up?"&nbsp;<br>
                    <br>
                    With stockpiles low, this would have to come from extra production. But even if producers could suddenly boost production to offset a supply shock, it would take weeks, if not months, for the crude to reach end users. However, the world is producing at close to capacity, with only the Saudi Arabia and, to a lesser extent, the United Arab Emirates boasting excess production capability. World oil production was 78.6 million barrels per day (bpd) in October, with spare capacity of around 2.6 million bpd.<br>
                    <br>
                    Ali Rodriguez, Venezuela's oil minister and new Secretary General of Opec, argues that the cartel has done enough to supply markets this year. "The true reasons for currently high oil prices lie behind a series of other factors not directly linked to crude oil supply." He argues that a lack of refining capacity in the United States and the shrinking of the world's oil tanker fleet have caused bottlenecks. He believes that high taxes in consumer countries are a major cause of inflationary high prices for energy consumers. And he has long argued that speculation by traders on markets such as the International Petroleum Exchange in London and the New York Mercantile Exchange are responsible for as much as $US8 on the price of a barrel.<br>
                    <br>
                    The IEA agrees that physical constraints are an issue. "Only so much crude can be shipped, stored and processed at refineries," it said in November. "Only so much product can be produced, transported and distributed to end-users. Lead times are long. Unless stocks are built in advance of peak demand regional supply imbalances are inevitable, as was the case with gasoline last summer and heating oil this winter."<br>
                    <br>
                    Against this background, what does 2001 hold in store? Just a few months ago, many analysts feared that Opec's generous output hikes would flood the market in Spring 2001, as peak winter demand eased off, leading to a price crash. However, it now appears that excess crude will be soaked up by the need to replenish stockpiles. "As a result of these tight world oil markets, world oil prices are expected to average $US30 per barrel during the first half of 2001," says the Energy Information Administration (EIA), part of the United States Government Department of Energy. The EIA expects prices to fall later in the year, but only slightly, closing 2001 at $26 per barrel. Opec ministers convene again on January 17, a meeting which is eagerly awaited by the market. But unless the cartel makes some big mistakes (and it must be remembered that it has dropped some real clangers in recent years), Opec governments should be set for another year of bumper returns.<br>
