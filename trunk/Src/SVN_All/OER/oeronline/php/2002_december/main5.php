                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">A gem of an idea</font><p><font face="Verdana" size="1"><b>Opal celebrated its first year of official recognition recently. As new members are invited to join Opal, OER investigates why they should&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_december/opal2.jpg" width="150" height="222" align="left" hspace="5" vspace="5">Oman Society for Petroleum Services (OPAL) is the first formal industry society of its kind in the Sultanate that became officially registered with the support of government just over a year ago. The members come from diverse fields ranging from catering to engineering and from financial services to construction, but what binds them together is the oil and gas industry they serve and their like-minded intentions to improve upon the standards of work competence and professionalism in all facets of the industry.<br>
                    Sixty members - including Petroleum Development Oman, Oman LNG, Oman Refinery Co., Oman Gas Co., Occidental, and Halliburton - have now recognised OPAL as a platform to build consensus on how to constantly improve the industry's level of Omanisation, training, employment practices, best business practices, community relations and health, safety and environment (HSE) in the Sultanate. Oman's government is optimistic that OPAL could become a fundamental channel for making progress on such topics in the oil and gas sector and many more companies have expressed an interest to join the organisation which was originally founded in 1998 as the ÔOil and Gas Contractors Forum'.<br>
                    <br>
                    <b>OPAL's prime objectives&nbsp;</b></font></p>
                    <ul>
                      <li><font face="Verdana" size="2">&nbsp;Promoting world-class standards  of commerce and professionalism</font></li>
                      <li><font face="Verdana" size="2">&nbsp;Addressing specific local challenges such as Omanisation</font></li>
                      <li><font face="Verdana" size="2">&nbsp;Seeking innovative and cost effective solutions to problems like maintaining the highest standards of Health, Safety and Environment (HSE)</font></li>
                      <li><font face="Verdana" size="2">Improve communication of best practices and ideas</font></li>
                    </ul>
                    <p><font face="Verdana" size="2"><br>
                    'Our strategy is getting the whole industry together, aligning our objectives and building consensus among all the players,' says Stephen Thomas, OPAL's Chairman.<br>
                    <br>
                    <b>Advantages of membership&nbsp;</b><br>
                    'Now is the right time to join Oman Society for Petroleum Services. OPAL members are convinced that it is the correct mechanism to move the oil and gas industry forward. By joining us, they are already reaping the fruit,' says Shahswar Al Balushi, OPAL's Chief Executive Officer.<br>
                    'A major incentive to join now is that you will be able to shape the industry. Through its early initiatives, Oman is demonstrating what can be achieved through private sector industry and government collaboration. Although this is an intangible benefit, the material gains are being talked about at the moment and these will follow for OPAL members.'  The more tangible benefits could include reduced insurance premiums for meeting OPAL's targets on its HSE programme or more streamlined labour clearances for example. Some industrialists, however, argue that OPAL should offer short-term benefits such as these now to attract members.<br>
                    'Personally, I feel these benefits should occur naturally. There is a clear danger of aiming too low and hitting the target rather than aiming as high as we can,' adds OPAL's CEO.<br>
                    <br>
                    A substantial number of milestones have already been achieved by OPAL. After obtaining funding from industry and government, a major achievement was getting officially registered as a society. It managed to do this last year after it had aligned the views of its stakeholders and defined the industry's main challenges. But OPAL isn't simply about tackling Omanisation and it could run into difficulty on then switching its focus among different objectives. Another challenge it faces is to ensure that certain OPAL members do not use the forum to table motions of priority to them and not others within the industry.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">
                    <b>Omanisation targets agreed</b><br>
                    'One of our major goals is to reverse the employment position that currently exists in Oman's oil and gas industry. We want to move to a ratio of two Omanis in the industry to one expatriate instead of having an Omanisation level below 35 per cent,' says Simon Karam, Vice Chairman of OPAL's Board.</font></p>
                    <p><font face="Verdana" size="2">
                    Industry experts say that part of the reason for the current problem facing the industry has been a competency mismatch where Omanis entering employment are not meeting the job requirements that are needed. Exact figures indicating the levels of employment in the oil industry are unavailable but the latest registered employment in Oman's total economy is 720,121, with only 20.9 per cent representing nationals. In the next five years, 248,050 people will enter the job market from schools and colleges but only 50,500 may find work, according to data from the Ministry of National Economy. Thus, Oman's jobless count could worsen by 395,100  by 2007. The country's unemployment level is estimated at 197,100 for 2002.<br>
                    <br>
 'These statistics tell us that failure is not an option. OPAL  must succeed in its Omanisation plans,' adds Stephen Thomas, Chairman of OPAL's steering committee. 'Under the current OPAL / government initiative, employers are investing in work-based training and the government is financing pre-employment technical competencies by giving nationals a pre-entry foundation in HSE induction, work ethic, basic language requirements and technical skills.'&nbsp;</font></p>
                    <p><font face="Verdana" size="2">
                    'In some of the economic sectors, replacing expatriates has been successful. For instance, the banking sector is one such example. The oil and gas sector is proving more difficult and that is why a mechanism like OPAL is needed,' says Karam. Government officials have consistently lauded the achievements in Omanisation by the oil and gas industry but have also urged the industry to find new opportunities for the indigenous population.</font></p>
                    <p><font face="Verdana" size="2">
                    Oman's government is supporting OPAL by financing its competency-enhancing programme. The Ministry of Manpower signed an agreement with OPAL on June 5, 2002 for RO2.0 million per year to raise the competency of 1,000 potential employees of the oil and gas sector every year for the next five years. The first phase of this joint government and private sector initiative from June 2002 to the end of July 2002 has secured 491 jobs. Between July 2002 and the end of the year, 495 jobs have been identified in the oil and gas sector and OPAL is currently in the process of mobilising trainees into these new positions by starting new training
                    programmes.&nbsp;<br>
                    <br>
                    'Omanisation is a critical issue for the nation. OPAL is not only going to be judged on its ability to facilitate training but it will also be assessed by the standards of those that have already been trained and are now in employment,' says Al Balushi who was appointed in April as OPAL's CEO.&nbsp;<br>
                    'This is not training for training's sake. Employment is the key, and training is the enabler,' says Karam.   He says training leads to full-time employment but further induction training will allow an employee to realise further career development.<br>
                    'The industry will not be recognised as world class until we have a lot more nationals working in the oil and gas sector. If all goes to plan, Oman could even export a significant amount of skilled labour abroad,' he explains.<br>
                    <br>
                    <b>Mapping out future success</b><br>
                    In November, 120 managers from 63 private sector companies attended an OPAL workshop to reach a common consensus among industry and government officials on how the local petroleum industry should tackle its most serious challenges.  OPAL plans to host a meaningful summit in the beginning of 2003 where key decision makers from industry and government will decide on how to implement new organisational policies and agreed industry minimum standards.</font></p>
                    <p><font face="Verdana" size="2">
                    OPAL was modelled closely on organisations such as Coral and Logic (formerly CRINE), which have served the Malaysian and UK oil and gas industries respectively. Both organisations are centred on cost-reduction such as driving best supply-chain management and efficiency levels without effecting HSE standards.&nbsp;<br>
                    'OPAL exists in a younger industry than Coral or CRINE and it must focus on the national imperative of Omanisation as well as getting people to a minimum standard of HSE and best practices. If we achieve success on these issues, OPAL can make faster improvements in efficiency drives as a result,' says Thomas.&nbsp;<br>
                    The tasks ahead are tough but OPAL is preparing to meet them. OPAL has already achieved a level of success in getting many different government departments to discuss single issues of critical importance to the industry as well as coaching international companies to think seriously about getting engaged in knowledge sharing and information exchange.&nbsp;<br>
                    The initial success of UK-based CRINE was stilted when officials representing different government ministries were unable to meet with it together on separate issues, says Thomas. OPAL will be looking to repeat the successes of CRINE in attaining cross-governmental participation and support to achieve major breakthroughs.&nbsp;</font></p>
