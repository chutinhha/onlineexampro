                    News</font><p><b><font face="Verdana" size="1">The 2003 Volvo<br>
                    launched in Omani market</font></b><font face="Verdana" size="2"><span style="mso-fareast-font-family: MS Mincho"><br>
                    <br>
                    MHD LLC has launched the 2003 Volvo model range in Oman with special Ramadhan offers. At a function held in the company's Azaiba showroom, Mohsin Haider Darwish, Managing Director of MHD, welcomed the Volvo brand into the company's automotive stable, which also sells two other leading auto brands - Land Rover and Jaguar.<br>
                    "I am honoured to welcome Volvo, the newest member of the Automotive Division into our fold. We are looking forward to working with such an exciting brand and the launch of the new Volvo model range marks a significant step for MHD's Automotive Division here in Oman," said Darwish. As the first shipment of the Volvo 2003 model range has arrived in Oman just in time for Ramadhan, MHD has chosen to celebrate both the launch and the Holy month with special prizes starting from RO5,995 for the Volvo S40. Other models from the Volvo range on display include the sporty S60 saloon and the luxurious executive saloon, the Volvo S80.<br>
                    The agreement between Volvo Car Overseas and MHD appointing the latter as the sole authorised dealer in Oman was signed recently. At the function, MHD's Automotive Division Director Robert Spikins invited all existing Volvo owners for a free oil and filter change. The company has also launched Volvo Parts Price Watch. "It's often a misconception that serving a car at the authorised dealer service centre is expensive. If a customer finds a Volvo part listed on a catalogue at any authorised distributor in the GCC at a cheaper price than ours, then we will refund the difference," Spikins said.<br>
                    <br>
                    The all-new, awe-inspiring new Prado has been unleashed on the market. Packed with raw energy, the new vehicle is equipped with power to overpower, claim the car's makers. They say it is also geared up to deliver silky smooth performance. Available with a choice of 2.7 litre petrol engine that delivers 157 horsepower at 4,800 rpm and the 3-litre diesel engine that offers 100 horsepower at 4,000 rpm.&nbsp;<br>
                    The all-new Prado offers never-before, high-end features which include the graphic display in the field monitor as well as a clock, navigational compass, outside temperature gauge, potential cruising time, altitude and atmospheric pressure combination meter - communicating driving information at a glance.&nbsp;<br>
                    Other exciting features include CD Player, dual auto air conditioners, and keyless entry. In addition, there's the added assurance of the new Prado possessing an anti-theft immobiliser system. The all new Prado also incorporates a number of advance active safety measures such as anti-lock brake system (ABS) with electronic brake force distribution (EBFD). Passive safety measures include SRS airbags, 3-point seat belts with pre-tensioners and force limiters, set the standards for safety. The differential lock gets activated at the touch of a button to release the wheels if the Prado ever is stuck.<br>
                    During Ramdhan, the local car dealers, Saud Bahwan Automotive LLC, are offering a plethora of benefits such as a competition where customers have the opportunity to win a car every day. New Prado owners will also enjoy three years free service (or 60,000 kms), a hamper full of goodies as well as many other value-added benefits that include six years protection and easy financing.</span></font></p>
