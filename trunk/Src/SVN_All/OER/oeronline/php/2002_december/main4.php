                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Gaining altitude</font><p><font face="Verdana" size="1"><b>Despite a bumpy takeoff, SWISS is confident that it will stay in Muscat and soar in profit.
                    OER reports</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_december/50-swiss.jpg" align="left" hspace="5" vspace="5" width="174" height="247"></font><font face="Verdana" size="2">No one imagined that Swissair, once a proud symbol of the Swiss nation, would go belly up and plunge into bankruptcy last October, after piling up heavy debts and a travel drought following 9/11. On March 31, 2002, the beleaguered airline disappeared from radar screens altogether. The void was filled by a new company called "SWISS", created from the merger of Crossair, a regional Swiss airline serving European destinations. It took over Swissair"s intercontinental routes after arranging around US$1.8 billion of financial support from Swiss industry and government. Despite the new name, logo, livery and financiers, it still has an uphill task to regain customer confidence.<br>
                    <br>
                    The airline has decided to continue operating in a difficult market like Oman, a move intended to signal that it is back in business. 'We"ve cut down on the global network, but Muscat is still on the map and this has given the airline a much needed boost here,' says Ruedi Seiz, Country Manager, SWISS, in Oman. A few months ago, the pullout of the Dutch airline KLM from the Sultanate sent out shock waves to investors in Oman"s ongoing airport expansions. Meanwhile, alarm bells began ringing that SWISS, which operates a return flight to Muscat from Zurich via Dubai three-times-a-week, would follow suit. But there are a number of reasons why SWISS decided to stay in Oman, chief among them being the attractiveness of the Sultanate as a tourist destination.<br>
                    <br>
                    'Our operation in the Gulf is reasonably efficient. We have a routing via Dubai with high load factor, and the segment between Dubai and Muscat does not add a cost that could jeopardise the operation,' explains Ruedi. 'In the past, Muscat has also made a solid contribution to the route despite the fact that there are fewer Swiss expatriates as a captive market, compared to what the case is with our competitors.'  The job of regaining government and customer confidence has been achieved here, says Seiz.<br>
                    <br>
                    He acknowledges a five per cent drop in passenger traffic this year, though that can be attributed to other factors. Oman has reported some relatively weak air travel statistics in 2002. Airport passenger figures for the first half of this year have shown 11 per cent decline while the export tonnage has shown about 30 per cent reduction.&nbsp;<br>
                    The new airline began commercial operations on April 1, 2002, but it"s the rivals who would be "April fools" if they regard SWISS as a fledgling airline. It has, however, shed about 30 per cent of the Swissair network. Currently it flies to 126 destinations in 60 countries with a fleet of 128 aircraft.&nbsp;<br>
                    <br>
                    SWISS posted a US$ 93 million loss for the third quarter of 2002. Having generated consolidated revenue of US$ 2.16 billion for the first nine months of 2002, the net loss stands at US$ 401 million the year-to-date. These losses have been less than what was expected, so the airline says it"s on track. Last month, an optimisation programme was announced. It will add US$275 million to its 2003 bottomline, and includes modifications to the route network from the start of the 2003 summer schedules, a downsizing of the fleet by eight aircraft (one long-haul, one medium-haul, three chartered and three 50-seat regional aircraft), a revised charter concept and enhancements to its in-flight   product. It will entail reduction of some 300 personnel, although around 200 new positions in the Technical Services and IT divisions are also planned.&nbsp;<br>
                    Compared to Swissair, the new airline flies to the same destinations in the Gulf but with slightly reduced frequencies. In the US, it no longer flies to Atlanta or San Francisco and has halved the flights to New York and Boston. This decrease has had a knock-on impact in the Gulf and other regions where there is limited availability of seats to North America, and demand exceeds supply despite the pangs of the industry since 9/11. Moreover, the new entity does not fly to former Swissair destinations like Shanghai, Taipei, Madras and Osaka.&nbsp;<br>
                    <br>
                    There are many challenges for SWISS to overcome and the media have painted a turbulent future for the airline saying that it has to avoid making mistakes in its infancy. It is expected to lose US$500 million this year, mainly due to start-up costs. An agreement has been reached with the former Swissair pilots on staffing, pay and work arrangements. But unresolved issues with the pilots of former Crossair, and the still unbalanced overall network, particularly in Europe, are making the journey towards profitability uncomfortable, according to analysts. 'Although confidence has been restored, we are not at cruising speed yet. We are still climbing. And we still have to merge two different corporate cultures,' says Seiz.<br>
                    <br>
                    The EuroAirport at Basel, the founding city of Crossair, is erupting as another battlefield for the fledgling airline. It is being said that SWISS is planning to transfer some connecting traffic from Basel to Zurich, which is located 80 kms away, and is the primary hub for Europe and intercontinental traffic for SWISS. But this would make Basel less meaningful in the SWISS route network and render its future uncertain. Operating both hubs economically is a problem. Analysts expect that shifting traffic from Basel to Zurich would invite considerable opposition and protests from local politicians and residents. 'A lot of energy is thus being used to solve nagging issues, when it could be better utilised for developing business,' reasons Seiz.'The challenge is to break even by 2003. It is a tall order. If we succeed, we should make a profit in 2004 as per our business plan,' he adds. The Swiss government having taken a front seat in the formation of the airline, the taxpayers will also be demanding performance.&nbsp;<br>
                    <br>
                    There was a continuing month-by-month increase in passengers during the first six months, with over five million passengers welcomed aboard. With a code-share agreement with American Airlines for the lucrative US market, SWISS is now pushing for membership in OneWorld, a global alliance of eight airlines that includes American Airlines and British Airways, according to media reports. If regulatory approvals are not granted, SWISS will negotiate agreements with individual carriers. In October, an "Open Sky" agreement was signed between Switzerland and Canada, giving the company unlimited access to all Canadian destinations and comprehensive flexibility for introduction of new fares. SWISS also re-established a code share agreement with Oman Air on its Muscat route.<br>
                    <br>
                    'In Oman, we have been attracting a first-class clientele since we deliver high quality service,' adds Seiz. According to newspaper reports, SWISS could be the best airline to withstand any regional instability, because passengers would perceive it as a "neutral" carrier and therefore less prone to terrorist attack. It is obvious that the company is also banking on a global economic recovery in the medium term along with some significant improvements in the performance of the local tourism and travel sector.&nbsp;<br>
                    <br>
                    It remains to be seen how Swiss will navigate past the teething problems and scale the dizzy heights. For the time being, it has at least shown that it is willing to take difficult decisions and manage costs very carefully.&nbsp;<br>
                    <br>
                    <br>
                    <b>Promoting Oman the SWISS way</b><br>
                    SWISS chose Muscat as their featured destination to travel to in their November issue of the airline"s inflight magazine. There are 15 pages in total devoted to highlighting tourist sights, culture and life in Oman. 'It was a very useful PR exercise and we"re all very pleased and proud of something that many believe has become a tool for promoting the country,' said Ruedi Seiz, SWISS"s Manager for Oman.<br>
                    <br>
                    <br>
                    <b>Swiss carriers in the Sultanate</b><br>
                    Oman was first plotted as a direct destination on Swissair"s travel network as early as the end of the Eighties with a weekly charter flight, operated by Balair from Zurich to Muscat and back each Saturday. In 1993 this charter was replaced by a regular service to Karachi via Muscat. 'The involvement of Swissair in Oman grew because the country had found its way on to Europe"s tourist map and was at the time, and still is, a niche product for tour operators catering to upmarket clientele in Switzerland,' recalls Ruedi Seiz, SWISS" manager for Oman. The frequency quickly grew to two flights per week and by winter 2000, Swissair was flying as many as four flights per week. Currently, SWISS operates a return flight to Muscat from Zurich via Dubai three-times-a-week.&nbsp;<br>
