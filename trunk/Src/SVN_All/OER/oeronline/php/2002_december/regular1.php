                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The balancing act</font><p><font face="Verdana" size="2">
                    <br>
                    <img border="0" src="../../images/2002_december/32-finance-1.jpg" align="left" hspace="5" vspace="5" width="200" height="200">Asset Liability Management (ALM)  has acquired greater significance in the finance sector, particularly in banking, with the mismatch between assets and liabilities widening. Typically, the major assets of a bank consist of loans and advances given to the public or corporates. On the other hand, the major liabilities consist of deposits that the public or organisations have placed in the bank, which the bank has to return when the deposits reach maturity.&nbsp;<br>
                    Generally, the assets have longer repayment period compared to the durations of the liabilities. Because of this, banks have to keep a close eye on ‘liquidity risk’. Further, they have to keep in mind that deposits have pre-closure options, whereas very few loans are repaid before time. Even such prepayments essentially involve swapping the existing outstanding loan with a new loan bearing a lower interest rate, with the maturity profile nearly remaining the same.<br>
                    With the globalisation of the economy, assets and liabilities have become more sensitive to durations, interest rates and exchange rates. ALM involves strategic balance sheet management aiming at managing liquidity risks, as well as risks caused by fluctuations in interest rates and exchange rates. While it is not possible to eliminate these risks, the objective is to control them at an ideal level.&nbsp;<br>
                    <br>
                    <b>Managing interest rates</b><br>
                    ALM significantly focuses on interest rate management and liquidity management. Interest rate management or pricing of assets/liabilities aims at optimising the interest spread available between funds raised through classes of liabilities and funds deployed through classes of assets. The process involves identifying rate sensitive groups of assets and liabilities, estimating the interest rate movements, valuing the assets/liabilities based on interest rate estimates and suitably planning for increase/decrease in the asset/liability categories to keep the risk under control, which is known as
                    'repricing of assets and liabilities'.<br>
                    Similarly, liquidity management aims at matching the various maturity periods of liabilities with that of assets. The process involves grouping assets and liabilities based on maturity periods, assessing the mismatch gap and planning for future financing requirements based on acceptable levels of mismatch under different maturity periods.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">
                    The economists of World Bank in an article in the December 2000 issue of the Finance And Development magazine. reviewed the financial crisis of East Asia. The authors observed that in the 1990s, Asian banks and financial institutions were borrowing heavily and rapidly building up considerable short-term debt. During 1997-98, these in turn, increased the vulnerability of many countries in East Asia to liquidity crises, as short-term borrowings tended to reverse more rapidly during adverse economic shocks. Based on the GDP data of 1986-98 for 33 developing countries, the authors observed that the rate of decline of short-term debt during an adverse GDP shock was twice as high as compared to the rate of increase during a positive economic shock. The analysis revealed that, in contrast, medium and long-term debt was weakly counter cyclical to GDP shocks. While this is the nature of impact on the economies of nations, the ALM concept’s criticality for organisations needs no further emphasis.<br>
                    <br>
                    <b>The Oman scenario</b><br>
                    Against this backdrop, let us now look at the picture in Oman. The consolidated mismatch position compiled from the asset/liability maturity profile data of the four listed banks in Oman (BankMuscat, National Bank of Oman, Oman International Bank and Bank Dhofar) as on Sept 30, 2002 is furnished in the graph.</font></p>
                    <p><font face="Verdana" size="2">I<img border="0" src="../../images/2002_december/32-Finance-2.jpg" align="right" hspace="5" vspace="5" width="200" height="220">t is seen that the negative mismatch (excess of liabilities over assets) position in the less-than-1-year category has increased from 22.95 per cent of total assets in September 2001 to 26.9 per cent in September 2002. This period is characterised by easy money conditions with falling interest rates. Under such circumstances, depositors wait for increase in interest rates in the future, and thus prefer to go in for short-term holdings. This accounts for the larger expansion in the liabilities of banks under short-term maturity.&nbsp;<br>
                    <br>
                    From the graph, it is also seen that the positive mismatch (excess of assets over liabilities) position in the over-3-years category has increased from 16.11 per cent to 22.09 per cent of total assets, whereas the positive mismatch in the under-3-year category has decreased from 6.84 per cent to 4.81 per cent during the same period. This is because borrowers tend to avail of the opportunity of soft interest rates by trying to increase the durations of their borrowings. This leads to larger expansion in the assets of banks under long-term maturity.<br>
                    Banks try to minimise these mismatch positions by suitably pricing the assets and liabilities. That probably explains the comparative larger fall observed in the short-term rates vis-à-vis the long-term rates (for both deposits and loans) during this period. During hard money conditions, the reverse happens due to change in the duration preferences of depositors and borrowers.&nbsp;<br>
                    As said earlier, bankers can at best try to keep the mismatch risk as low as possible by appropriate pricing policies, as ALM practices also have to take into account the objectives of business growth and profit maximisation. The impact of market dynamics can be cushioned, but can never be fully neutralised.&nbsp;<br>
