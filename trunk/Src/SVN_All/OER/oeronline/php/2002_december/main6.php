                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Boardroom safeguards<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>In the Sultanate, the amended Commercial Companies Law and the new Code of Corporate Governance are landmark developments for the business community. Based on a KPMG analysis, OER enumerates the salient points&nbsp;&nbsp;</b></font>
                    <p><font face="Verdana" size="2"><b>Commercial Companies Law (CCL)</b><br>
                    Directorships: A person now cannot be a director of more than four or a chairman of more than two public joint stock companies, as against earlier provisions of directorships of five and chairmanships of three such companies. The law prohibits a person from simultaneous membership of the board of directors of a public joint stock company and a closed joint stock company engaged in similar activities.<br>
                    <br>
                    <b>
                    Election of directors:</b> Earlier, any person who held at least 10 per cent shares could be a director. The amended law says that the directors of the board shall be elected from amongst the shareholders or from outside. If from the shareholders, then he should hold the minimum shares as specified in the articles of association. The Minister of Commerce and Industry has been empowered to issue regulations on the election of directors. This amendment will be effective only when the first election is held after October 1.<br>
                    <br>
                    <b>
                    Internal regulations/systems:</b> The board of directors is now required to formulate before October 1, 2003, regulations/systems governing its management, business and personnel affairs. For new companies, the internal regulations should be formulated within one year from the date of registration of the company.<br>
                    <br>
                    <b>
                    Delegation of responsibility:</b> The law earlier allowed the board of directors to delegate its responsibilities to its chairman or managers or committees constituted by the board. The CCL now authorises delegation only to the committees.&nbsp;<br>
                    <br>
                    <b>
                    Audit:</b> The board of directors are now required to form an audit committee from amongst its members, and the board of directors are to appoint an internal auditor and a legal consultant in accordance with the Code of Corporate Governance.&nbsp;<br>
                    <br>
                    <b>
                    General meetings:</b> It is now clarified that public joint stock companies have to notify the Capital Market Authority (CMA) about their general meetings whilst closed joint stock companies have to notify the Ministry of Commerce and Industry. They may depute an observer to attend and supervise the proceedings. The chairman of the board of directors will have to himself preside over the general meeting and if that is not possible, his deputy elected in accordance with the provisions of the CCL will preside. The minutes have to be lodged within 15 days.<br>
                    <br>
                    <b>
                    Increase in share capital and rights issue:</b> The amended CCL provides that additional shares of a company’s capital can, with the approval of the extraordinary general meeting, be allotted to a specified person or persons in accordance with the rules issued by the CMA. In the case of a public subscription, an existing shareholder would have a preferential right to subscription. Previously, in the case of an under-subscription, a company had to re-offer the un-subscribed shares to the existing shareholders. Now companies can either offer the shares to the public or reduce the capital corresponding to the face value of the un-subscribed shares. Existing shareholders can also assign their preferential right to new shares.<br>
                    <br>
                    <b>
                    Lock-in period for founder members:</b> Promoters earlier could not  withdraw or dispose off their shares before the company had published financial statements for two consecutive financial years. It is now clarified that the two financial years will be from the commencement of actual production or operations. Further, the Minister of Commerce and Industry can extend this restriction for another year, if so requested by the CMA.<br>
                    <br>
                    <b>
                    Negotiable bonds:</b> Negotiable bonds can now be issued by joint stock companies either to the public or to one or more specified persons.<br>
                    <br>
                    <b>
                    Capital of a limited liability company (LLC):</b> The Director General of Commerce has been empowered to lower the minimum capital of certain LLCs from RO 20,000 to RO 3,000, subject to the rules to be issued by a decision of the Minister of Commerce and Industry. Earlier, an interested party could serve a notice to the company to restore its capital in case it fell below the minimum of RO 20,000, and could request the Commercial Court to order dissolution in the case of failure to restore the capital within one year from the date of notice. Now this right has been extended to all cases in which the capital falls below the registered share capital (not just below RO 20,000).<br>
                    <br>
                    Company transformation: Earlier, a company could be transformed from one form to another only after it had issued three audited annual financial statements. This provision has been deleted. However, the Minister of Commerce and Industry has been empowered to issue a decision stipulating conditions for conversion to a public joint stock company.<br>
                    <br>
                    <b>Code of corporate governance</b><br>
                    Scope and applicability: Prescribing only the minimum mandatory requirements for all publicly listed companies and mutual funds, it comes into immediate effect.<br>
                    <br>
                    Composition of the board of directors: Majority of the board should comprise non-executive directors (who are not  whole-time director and do not draw any fixed salary).</font></p>
                    <ul>
                      <li><font face="Verdana" size="2">One-third of the board should comprise independent directors (who are non-executive directors without any material pecuniary relationship or transaction with the company and its group, its significant shareholders and its management. None of their relatives must have been a key employee of the company or its group during the last two years).</font></li>
                      <li><font face="Verdana" size="2">Non-executive and independent directors should be identified in the annual report.</font></li>
                      <li><font face="Verdana" size="2">The chairman of the board cannot be the chief executive.</font></li>
                    </ul>
                    <p><font face="Verdana" size="2"><b>
                    Board meetings:</b> The board must meet at least four times in a year with a maximum interval of four months. The number of board meetings held must be disclosed in the company’s annual report on corporate governance. The functions of the board of directors are outlined in Article 5 of the Code.<br>
                    <br>
                    <b>
                    Audit committees:</b> The board should constitute an audit committee of at least three members, all of whom should be non-executive directors and a majority being independent directors, with at least one member having finance and accounting expertise. The committee will hear the external auditors before forwarding the annual financial statements to the board for approval, and also recommend the external auditors to the board for approval by the AGM.<br>
                    <br>
                    Related party transactions and disclosures: Management is required to disclose to the board all transactions in which they, or their relatives, have a personal interest that may be in conflict with company interests.<br>
                    <br>
                    <b>
                    Annual reports:</b> The directors have to review the internal control systems at least annually and report to the shareholders. The annual report should include a separate section on corporate governance, covering implementation and highlighting any non-compliance. The report should also include a Management Discussion and Analysis.<br>
                    <br>
                    <b>
                    External auditors:</b> Auditors are to be appointed for one financial year at a time. Beyond four consecutive financial years, the audit firm will be eligible for reappointment only after a cooling-off period of two years. This provision applies to financial years ending on or after January 1, 2003. Companies with a December year-end can continue with their current auditors, but would need to rotate them in 2004. The auditors are to report separately on any material misrepresentations or any significant concerns that come to their attention. They are not to be engaged in non-audit services and must always consider matters relating to their independence.<br>
                    <br>
                    With significant emphasis on proper management and governance, the amended Companies Law and the Governance Code have highlighted the responsibilities of the Board of Directors in ensuring transparency and maximising shareholder value.&nbsp;</font></p>
