                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Trading views</font><p><font face="Verdana" size="1"><b>The recent Gulf Economic Forum saw an increasing consensus on the need for economic reforms and further integration of the regional and global trade. Saleh Al Shaibany brings a ringside view of the heated debate at the conference</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_december/38-GccHE.-Al-Macki.jpg" align="left" hspace="5" vspace="5" width="200" height="146">The Gulf Cooperation Council (GCC), which comprises of Oman, Kuwait, the United Arab Emirates (UAE), Bahrain, Qatar and Saudi Arabia, faces its sternest test in its 21-year-old history amid mounting economic pressures to converge with powerful markets. In this backdrop, the GCC countries are busy framing an economic reforms agenda and working towards lifting various trade barriers to sustain growth in the wake of rapid global changes.</font></p>
                    <p><font face="Verdana" size="2">&quot;GCC countries seek to qualify and merge their economies globally by eliminating restrictions, formulating essential legislation and taking appropriate measures for supporting local and international trade competitions," Ahmed bin Abdulnabi Macki, Oman’s National Economy Minister, told delegates of the Gulf Economic Forum held in Muscat last month.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">
                    The Gulf countries have been negotiating with the European Union (EU) to reduce import duties on GCC commodities, but so far the Europeans have refused to do so until some hurdles have been lifted. "The EU has made very clear that the agreement will be made after the customs union, which I expect to take place in the next six to nine months time," Maqbool bin Ali Sultan, Oman’s Commerce and Industry Minister said. The Gulf states agreed in December 2001 to establish a customs union and lower tariffs on foreign imports to five percent from the current duties of up to 15 per cent from January 2003.<br>
                    <br>
                    <b>EU back-pedalling</b><br>
                    But there are other hurdles too for a free trade deal between the EU and the Gulf states. European Commission Chief Economic Adviser Andre Sapir said a customs union alone would not be enough for the two blocs to sign a trade agreement. "The expectation is to have a customs union and Saudi Arabia become a member of World Trade Organisation (WTO), then the EU-GCC negotiations will take place," Sapir said.<br>
                    Oil superpower Saudi Arabia, one of the world’s four largest economies still outside the WTO, is the only Gulf Arab state in the six-nation GCC yet to join the WTO. Riyadh-based economists and foreign business leaders say the lack of clarity in the Saudi legal system was making it hard for the conservative kingdom to join the
                    WTO.</font></p>
                    <p><font face="Verdana" size="2">
                    Saudi Arabia could become a member late next year after the WTO ministerial meeting which is scheduled to take place in the autumn of 2003 in Mexico. A key dispute between the two sides is over a six percent EU duty on primary aluminium Gulf exports. Two smelters in Bahrain and the UAE produce more than one million tonnes per year and Oman is planning to set up a third smelter in the Gulf. The GCC officials said the EU was still dragging its feet on a trade pact. "We are negotiating with the EU as a bloc and not one country," GCC Secretary-General Abdulrahman Al-Attiya said.</font></p>
                    <p><font face="Verdana" size="2">
                    "As soon as we have fulfilled one condition, they come up with another. We don’t see any link between Saudi Arabia being WTO member and the agreement," a UAE trade official, who was taking part in the Gulf Forum, said. The EU is the GCC’s biggest trade partner and an agreement would increase joint trade between the two alliances by 40 per cent from the current trade worth about US$46 billion annually.<br>
                    For over thirty years, the GCC states have used their formidable oil income to transform the region into modern countries but experts said that it was time the Gulf accepted the demands of the international markets.&nbsp;<br>
                    " It is commendable how the GCC turned the desert into great cities in such a short time but the countries need to also look beyond the domestic success," Dr. Vahan Zanoyan, Chief Executive Officer of U.S.-based Petroleum Finance Company said. "There are genuine concerns that the Gulf will be left behind if it does not lift some protectionist restrictions on some of their products," Zanoyan added.<br>
                    GCC states have been granted special status by the WTO to impose high duties on fish and agricultural imports to protect the local products.&nbsp;<br>
                    <br>
                    <b>Acquiring economic efficiency</b><br>
                    The Gulf states long-accustomed to relying on the vast oil income have now realised that the commodity is vulnerable to changes in global politics and economy, both in peace and war times. "Oil market instability brought about several challenges for the economies of GCC countries like budget deficits and the balance of payments," Macki said.<br>
                    The Gulf states have projected a combined budget deficit of US$21.32 billion in 2002 compared to US$8.54 billion last year, an increase of nearly 150 per cent. For the GCC countries to compete effectively with international markets, Macki insisted that the region must put its economic house in order.<br>
                    "The gulf countries need to make more coordinated and integrated efforts for developing their capabilities and establishing efficient and competitive economies. This further requires the development of institutions with a focus on technology and research, water and food security, besides improving bilateral trade relations," Macki emphasised. Macki also urged the GCC to increase intra-regional trade.</font></p>
                    <p><font face="Verdana" size="2">
                    According to the Ministry of National Economy statistics, Oman’s trade with other GCC states increased by 10.5 per cent to RO1.14 billion in 2001 compared to RO1.03 billion a year earlier. The UAE has been Oman’s biggest trade partner since 1999 in the internal transactions followed by Saudi Arabia.&nbsp;<br>
                    <br>
                    <b>More joint investments needed</b><br>
                    Attiyah called for more joint investment to promote economic growth, support regional cooperation and develop productive enterprises within the framework of their economic and political alliance.&nbsp;<br>
                    The region formed Gulf Investment Cooperation (GIC) in 1983, an investment arm of the six GCC countries, as a joint company to sustain economic growth for member states. GIC now holds stakes in 36 companies in the Gulf Arab region, while its worldwide assets topped US$5.0 billion by the end of 2001. It also plays the role of an aggressive promoter to lure foreign investments into the Gulf states.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">
                    A GIC study favours an increased focus on financial services, manufacturing, and energy and power sectors. The latter is of major interest for GIC’s growth plans with some US$200 billion of new investments needed to meet steadily rising power demand.<br>
                    GIC net income after provisions in 2001 represented a return on capital of 11.4 per cent and a return on equity of 8.3 per cent compared with 8.6 per cent in 2000. Shareholders’ equity fell in 2001 to US$1.152 billion from US$1.3 billion while paid-up capital by end-December was US$750 million.<br>
                    <br>
                    <b>Funding the region</b><br>
                    One of the biggest investment hurdles in the region is foreign business ownership. The GCC states require a local sponsor or partner for any foreign investment in the region. Analysts have called for such restrictions to be lifted to pave the way for freer trade. "Why should not a foreigner have 100 per cent ownership? These are the kinds of reforms the GCC needs to put in place to attract foreign investment in a big way," one economic analyst said.<br>
                    <br>
                    Regional business leaders said the GCC states are not aggressive enough when it came to business innovations and they urged local entrepreneurs to invest more at home.<br>
                    "There has been little or no new business creation since the 1970s. The economic boom we have seen since then was almost due to the discovery of oil, which is some accident of fate rather than inspiration or ingenuity," Mohammed Sarhan, Managing Director of Dubai-based
                    Amwal.com, said.<br>
                    <br>
                    Sarhan said that if the GCC needed to make impressions on major economies and forge ahead a formidable partnership, it must open up its own market and promote transparency. "Nothing short of that will make a telling difference. How can we expect the EU to take us more seriously if we are ignoring some fundamental trade rules?" He said the Gulf already has a business model for the rest of the region to follow. "If we can agree that a place such as Dubai has been able to create a more liberal, open economic system, where people are more likely to invest, then why can’t the rest of the region learn from this?"&nbsp;</font></p>
