                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Third	quarter	woes</font><p><font face="Verdana" size="2"><b>A disappointing third quarter performance has dampened the market sentiment. Only fresh inflows of funds and entry of new market   players would boost the activity in MSM, writes Saleh Al Shaibany</b><br>
                    <br>
                    </font><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">The Muscat Securities Market index showed little improvement after a lacklustre performance by corporates in the third quarter, while the cautious investors are still worrying over the long-term prospects of the market. The index rose just 1.6 percent after the third quarter results compared to the 14.5 percent rise after the second quarter results.&nbsp;<br>
                    <br>
                    'It has been a disappointing quarter with minor gains. Investors have been careful, keeping the index within the cautious zone of around 180 points,' says Nasser Al Mugheiry, a financial analyst at Abu Timam Financial Consultancy.
                    'The nine-month results are not that great and this is probably why the index has not gone up significantly. There are very few good stocks around and investors are forced to trade the same shares every week,' Mugheiry adds.<br>
                    <br>
                    On the contrary, supported by encouraging second quarter financial results, the index had reached its highest point of the year in July to 189.85 points from 165.81 points, the lowest in that quarter. Investors have not quite reacted the same way in the third quarter and the index inched up to 183.023 points on November 17 from 180.161 points in mid-September.<br>
                    <br>
                    Net profits from companies like Topaz Energy and Marine and Bank Muscat soared over 60 percent in the first nine months of 2002 compared with the same period last year. However, National Bank of Oman and Oman Fibre Optics disappointed many investors after their net profits plunged by over 80 per cent. There were steep losses as well from companies like Raysut Cement and Oman National Livestock, which suffered two consecutive losses in the two periods.<br>
                    <br>
                    Economists said that the stock exchange needed some fresh injection of funds to boost investor confidence.
                    'We cannot just rely on quarterly results to lift the MSM and then see it dropping soon after the steam has boiled over. We want to see new players in the market like what is happening to BP or Al Maha,' says Ahmed Al Hadhrami, Economist at Oman Holding International.<br>
                    <br>
                    BP PLC has agreed to sell its 49 per cent shares in its subsidiary BP Oman to state-run Oman Oil Company. Private Omani shareholders will retain the other 51 per cent shares of BP Oman. Oman's Ministry of Finance said last month that it would sell up to 40 per cent of its 65 per cent stake in Al Maha Petroleum Products Company, an investment arm of Oman Refinery Company, to the public. Abu Dhabi's ABS Lubricants owns the other 35 per cent of Al Maha, which operates 84 petroleum service stations in Oman.<br>
                    <br>
                    Investors argue that some listed companies are not really taking disclosure rules seriously, casting a long shadow over the MSM's performance.
                    'We have not seen a satisfactory conclusion of Ali Redha Trading problem. Banks still are not very clear how the company got its hands on such a large sum of money and where it all went,' says an investor.&nbsp;<br>
                    <br>
                    Ali Redha Trading collapsed earlier this year, affecting the profitability of some banks in the country after the firm was forced into liquidation as a result of its crippling loan that amounted to RO50 million.
                    'It is also not very clear how effective are the CMA's disclosure regulations. It has plugged some loopholes in the Commercial Law but we feel it does not have the resources to monitor violators who still withhold vital information from the public,' says the investor.&nbsp;<br>
                    <br>
                    The Capital Market Authority (CMA) beefed up transparency rules and introduced various reforms since 2000, including appointing a new director general for the MSM, changing the price index and transfering companies which lost more than 50 per cent of their capital to the third market.
                    'If we want to achieve the desired goals from corporate governance, the regulatory bodies must have the necessary power and tools to enforce punishments and penalties taken against the violators,' says Yahya Al Jabri, the CMA President.<br>
                    <br>
                    The CMA chief appealed to corporate leaders to help maintain investor confidence and boost the market.
                    'We must promote the concept of self- conscience, which is a fundamental element for the success of any regulations for corporate governance. From the past experience of global markets, the collapse of companies like Enron, suggests that the rules and regulations alone will not guarantee proper performance and success of the companies, unless the people who run the company realise their responsibilities. We also need to emphasise the importance of credibility,' Al Jabry added.<br>
                    <br>
                    More foreign investment needed<br>
                    Although the government has taken significant steps for inviting foreign investors into the country, the MSM has not really managed to capture the mood.
                    'Corporate governance is not only important to attract long-term foreign investments, but also an efficient instrument to expand the local markets by attracting local investors through proper legislative and regulatory frameworks,' the CMA President added.<br>
                    <br>
                    Combined foreign investments in the capital of all listed companies rose only marginally to 14.31 per cent in 2001 from 10.63 per cent in 1995.
                    'The statistics indicate that the bourse does not seriously attract foreign funds,' says an analyst.
                    'This shows that we need to change our objectives to put more efforts in raising foreign investment.'&nbsp;<br>
                    <br>
                    Bankers blamed the global economic turmoil as the reason for the lack of enthusiasm in foreign investment.
                    'The world's economy has been in a pretty bad shape in the last couple of years. The events of September 11 and now the tensions involving the U.S. threats over Iraq have also not helped either,' a BankMuscat financial analyst said.
                    'However, one can be confident of a turnaround only once the government-sponsored privatisation programme takes shape,' he added.<br>
                    <br>
                    The private company, which took over the management of Seeb and Salalah airports, is expected to sell 25 per cent of its capital to the public in 2005. The companies which run private power projects like Dhofar, Al Kamel and Barka too are required to float shares to the MSM five years after they have started their operations. The same is expected from the proposed privatisation projects of Omantel and Sohar Port.&nbsp;<br>
                    <br>
                    Role of brokerage companies&nbsp;<br>
                    Experts insisted that the brokerage companies are not strong enough to steer the market in the right direction.
                    'Most of our brokerage firms are weak in terms of capital and financial strength. This seriously prevents them from hiring experienced analysts and traders to advise investors,' says Al Hadhrami.<br>
                    There are 21 authorised brokers in the MSM but many feel that they have largely ignored the government's call to merge to strengthen their financial positions. In 2000, the government offered a five-year tax haven and a cash incentive of fifty per cent of the capital of brokerage firms that would merge. With the exception of the three brokerage operations run by BankMuscat, NBO and Oman Arab Bank, the shares     of the rest of the brokerage firms have struggled in the third quarter.<br>
                    <br>
                    'Banks have the resources to keep the brokerage business on the right track but there are question marks on smaller companies which struggle to make ends meet,' says Al Hadhrami. The most frequently traded shares of brokerage companies (not including brokerage branches of banks) did not make any significant gains in the third quarter, mainly because they did not post healthy net profits in the past. Al Shalman Securities Company made a small profit of RO154,000 in the first nine months of 2002 compared to RO15,000 a year earlier. Gulf Investment Services registered a profit of RO344,000 after a loss of RO143,000 in the January to September period of 2001.&nbsp;<br>
                    <br>
                    'We can only look at the year with cautious optimism and hope that the last quarter of the year would be better than the third quarter. Again, it will all depend on financial results rather than anything else,' says
                    Mugheiry.&nbsp;
                    </font>
                    </span></p>
