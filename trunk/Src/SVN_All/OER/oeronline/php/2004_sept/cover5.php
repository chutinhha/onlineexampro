            the market. So at a critical time for the oil market, is Russia a 
            help or a hindrance?</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im40.jpg" align="right" hspace="5" width="229" height="137">The former Soviet Union, led by
Russia, has boosted oil exports
considerably in recent months in
response to strong demand, and
yet concerns over the future of Russian
oil major Yukos have boosted record
high oil prices. So is Russia a potential
help to the oil market at a critical time –
or a problem waiting to happen?
Probably, it is neither.
<br>
<br>
At a time of unprecedented high
international oil prices and concerns
over supply tightness, the EIA reported
in August that the former Soviet Union
(FSU) had ramped up crude and oil
products exports sharply in July, to 7.89
million barrels per day (bpd) from 7.48
million bpd in June. The drive was led
by Russia, which according to the federal
energy ministry produced 9.33 million
bpd of crude in July, up from 9.26
million bpd in June. On the basis of data
from the state pipeline monopoly,
Transneft, and oil traders, it appears
that Russia exported some 5 million bpd
of crude oil in July.
<br>
<br>
The fact that that Russia and the
other FSU oil producers (principally
Kazakhstan and Azerbaijan) are increasing
exports—up by 720,000 bpd in July
2003, on IEA data—is particularly welcome
given the current state of oil markets.
Yet, while Russia is raising output
and exports, it is also a source of concern
for the markets. In early August, fears
over the output of the country’s largest
oil producer, Yukos, added to anxieties
over supply tightness. Yukos produces
1.7 million bpd and exports up to 60 per
cemt of this, mostly as crude oil.
However it has been subject to a politically
directed attack by the tax authorities,
which has landed the company
with a Rb99 billion ($3.4 billion; RO1.31
billion) bill for fines, penalties and
unpaid tax from 2000. A similar bill for
2001 is now in the pipeline and others
for 2002 and 2003 seem set to follow.
Unable to pay the 2000 bill, Yukos has
seen its bank accounts frozen while
bailiffs have seized
shares in its main production
subsidiaries in
preparation for a stateconducted
fire sale. Thus
the company has on several
occasions publicly
said its export volumes
are at immediate risk.
</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Pump it up:</b> Can
Russia come the oil market’s
rescue? The country
is the world’s second-largest exporter
of oil and for the past year has been
running neck and neck with Saudi
Arabia for the title of the leading crude
producer. Russia has raised crude output
from just over 6 million bpd in 1998
to 8.42 million bpd in 2003, and since
April 2004, it has produced over 9 million
bpd – an impressive rise of 50 per
cent in just six years. Over the same
period, domestic consumption has been
virtually flat, ranging between 2.46
million bpd and 2.52 million bpd. As a
consequence, virtually all of the output
gain has been exported.
<br>
<br>
The rise in exports has certainly
been assisted by high oil prices in the
past few years, creating a powerful
incentive for Russia’s privately owned
oil companies to increase production,
although this has been achieved in the
face of infrastructure constraints.
<br>
<br>
The lion’s share of Russia’s oil exports
are delivered via state monopoly Transneft, which owns and controls a
network of pipelines linking Russia’s
oil producing regions with refineries
and the main oil export terminals on
the Black and Baltic Seas. Transneft has
raised its capacity in recent years –
from 3.1 million bpd in mid-2001 to 4.1
million bpd in mid-2003, according to
Moscow brokerage United Financial
Group. But this is insufficient to carry
all the crude Russian companies produce
for export, even before quotas
given to Kazakhstan reduce the effective
capacity. Thus, the companies
have in recent years made extensive
use of rail and river transit. In 2002-03
traders estimated that some 600,000
bpd of exports bypassed Transneft; by
mid-2004, the estimate had risen to
around 1 million bpd.
<br>
<br>
So is Russia in a position to keep
raising production and output, thereby
calming the market? The great leap in
exports seen in July over June, amounting
to 720,000 bpd across the FSU on
IEA calculations, would appear to offer
some hope – yet it is almost certainly a
one-off. Russian oil companies were
desperate to book as many exports as
possible in July, as from August 1, they
have been obliged to pay a sharply
higher rate of duty on crude exports –
$69.90 (RO26.88) per tonne, compared
with $41.60 (RO16) previously.
&nbsp;<br>
<br>
This alone suggests that the July
export hike is not sustainable. Indeed,
the higher duties may lead to a cut in
exports – traders reckon that rail shipments
to small Black Sea and Baltic
ports such as Theodosia, Odessa,
Butinge and Ventspils would have
become unprofitable from August 1. On
the basis of data for the year to date, this
implies an export fall of some 120,000-
160,000 bpd. However, domestic prices
will quickly collapse if these volumes
are re-routed to the Russian market,
which is not large enough to absorb a
major increase in supply. At that point,
oil companies will be forced back into
the export market, at least until they can
revise production plans.
<br>
<br>
Moreover, Russia has little scope to
increase exports because of infrastructure
and logistical limitations. Transneft, Russia’s oil pipeline monopoly,
has succeeded in raising export
capacity in recent years by addressing
bottlenecks in its own system, opening
a new pipeline to the Baltic port of
Primorsk, and increasing the capacity of
that Baltic route (from an initial capacity
of 240,000 bpd, it now stands at 1 million
bpd). Yet, it seems that Transneft is
running out of quick, cheap fixes to
increase the system’s operating capacity;
the company’s head, Semyon
Vainstok, admitted this in May and one
of his deputy’s confirmed it in late July.
Export data back this up: despite an
increase of over 300,000 bpd in Russian
crude output from March to July,
exports via Transneft actually fell back
slightly from 4.09 million bpd in March
to 4.05 million bpd in June and 4.02 million
bpd in July.
<br>
<br>
Other routes for exporting oil—railways
and rivers—are also operating
near to full capacity. Just as importantly,
these routes are only practical when
oil prices are high. When the price of a
barrel of Urals blend returns to its trend
level, oil companies will have little or no
incentive to send crude thousands of kilometres by rail carriage or barge.
While a price collapse seems unlikely
this year, Russian exports will nevertheless
come under pressure from September/
October as the government, via
Transneft, will oblige the oil companies to make greater provision for the 
domestic market ahead of the winter heating season. At the same time, production 
growth in the Arctic regions will scale back as the weather there deteriorates. 
Thus, from the perspectives of production and export capacity, Russia is almost 
certainly unable to increase markedly its exports of crude beyond July levels, 
regardless of the fevered conditions on international oil markets.</font></p>

