            initial years in India, I was posted in Algeria, where I started my 
            career as a service engineer in 1983 and left Algeria in 1994 as 
            director of Tata Exports Liaison Bureau. Then, I had 2 wonderful 
            years in the HR industry in India, which was my learning curve 
            before joining Volvo's start-up operations in Bangalore. I spent 
            five years in the sales and marketing function at Volvo India before 
            joining Towell Auto Centre (TAC) as general manager in December 
            2001. <br>
            <br>
            <b>Please enlighten OER readers about the management structure of 
            TAC. </b><br>
            We have a very flat structure - I head the operations and report to 
            the TAC board. The profit centre heads of different streams (headed 
            by national managers) and the branch managers (who administer the 
            branch operations) directly report to me. One of the family members 
            is working with me closely to assist me in day-to-day operations, 
            who also handles the Human Resource and Administrative function 
            directly. <br>
            <br>
            <b>What does your role in the company entail? </b><br>
            As head of operations, I am supposed to understand the nitty-gritty 
            and bring profitability to the company. Things have been going well 
            for the past 4-5 years. Since 2002, we have really leaped forward 
            multiple times. Today, looking backwards, you can certainly say that 
            TAC has come a long way in the last two years, and that, like any 
            head of operations, I am supposed to operate the existing business, 
            grow it continuously and strategise with the team to reach a 
            selected position in the market without sacrificing stakeholders', 
            customers' and employees' interests. Since 2002, we have grown 
            considerably on all the fronts, which brings a lot of professional 
            satisfaction to me. <br>
            <br>
            <b>How long have you been in Oman, and how different is it from 
            handling operations in India, where you hail from? </b><br>
            I came to Oman in December 2001 from Volvo, in India, which was a 
            Greenfield operation. In Volvo, I was handling the sales, marketing 
            and business development function and not the overall operation. 
            India is a very big country, and the market at that time was growing 
            phenomenally. To grow with the market, with such a product, was easy 
            even though it took lot of efforts from our side to introduce a new 
            concept Heavy Commercial Vehicle, which cost three times the nearest 
            competitor. Travel used to take most of my time, and I used to spend 
            nearly 20 days a month away from home. Moreover, I was with the 
            principals, doing direct marketing, which was a different kind of an 
            assignment. <br>
            <br>
            On the contrary, Oman is a small country and a small market with 
            moderate growth where all the world-class products compete severely, 
            leaving no room for the rest. Here, for the first time in my life, I 
            am with a distributor (marketing company), handling complete 
            operations, putting all I have learnt in my life to test. As in all 
            marketing companies, the focus is more on the bottom line. My 
            quality of life has greatly improved here since I spend sufficient 
            time with my family. <br>
            <br>
            <b>Briefly describe your management style. </b><br>
            I manage by consensus. I have my management committee members--who 
            evaluate proposals and thrash the issues out among themselves. I 
            like opposing views and, often, call upon the team to challenge my 
            thinking when I moot a proposal. The bottom-up approach that I 
            strongly believe in and practice has helped to motivate the team and 
            the results are evident. <br>
            <br>
            <b>What are the challenges that your business faces in Oman? </b>
            <br>
            As I said earlier, it is a small market that is growing very 
            moderately and the world is here. Most of the competitors have taken 
            their positions, and to gain market share here is a big fight. 
            Despite this, we have shown tremendous growth in the last two years 
            and to maintain the same level of growth is a big challenge for my 
            business. <br>
            <br>
            <b>What were the challenges specific to TAC that you faced, and how 
            did you overcome these? </b><br>
            When I took over the operations, the bottom line of the company was 
            weak and the perception of TAC in the market was not encouraging 
            either. Moreover, the team was also not pretty much in place and 
            their motivation levels were very poor. <br>
            <br>
            Since nothing can be achieved without a good team, I started with 
            the team, and put the right person at the right level and removed 
            some of the non-performers to send out a clear message. Two-way 
            communication was established between the team and the management, 
            and everyone was clearly told what is in store for them if they put 
            in their best efforts. <br>
            <br>
            After that, we started working on the market perception, invested in 
            infrastructure, expanded our retail network, spent money on branding 
            and focused on a customer satisfaction exercise. In the ensuing 24 
            months, we achieved our first set of goals. Some of our principals 
            came up with better products, which helped us greatly to fight in 
            the marketplace. <br>
            <br>
            <b>What's your take on the spiralling global oil prices? Do you 
            think such high prices are sustainable in the long run? </b><br>
            We do sell Total lubricants - so we keep a tab on the crude oil 
            price, which has a direct bearing on the base oil price. The crude 
            oil price now is almost inching towards $50 a barrel and it is 
            obviously not going to sustain at those high levels since it will 
            affect most of the growing or developed economies. It is a temporary 
            phenomenon according to me.<br>
