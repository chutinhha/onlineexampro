<p align="justify"><font face="Verdana" size="4" color="#FF0000">The blame game</font><font face="Verdana" size="1"><br>
<br>
<b>Everyone in the oil industry is blaming each other for recent price peaks. By 
Richard Dean</b></font></p>

<p align="justify"><font face="Verdana" size="1">The global oil industry is 
consumed by a debate over who's to blame for the recent surge in oil prices. 
Most people within the industry agree that oil at $49/b is generally a bad 
thing. But there is little consensus about who should carry the can for sending 
crude prices to record levels in August. OPEC blames oil traders for driving 
prices up by speculating on derivative markets. Traders and analysts blame OPEC 
for failing to invest in significant capacity in recent years. Shareholders in 
Russian oil major Yukos blame President Vladimir Putin for staging a witch-hunt 
against its boss, Mikael Khodorkovsky. Americans are beginning to blame their 
own President, George W. Bush, for refusing to release the country's Strategic 
Oil Reserve. <br>
<br>
Who's right? In truth, none of the above can be considered the culprit. But the 
reality—that fundamental political and economic factors have combined to drive 
up prices on world markets—is far too bland an explanation for the bloodthirsty 
critics within the energy industry. Every story needs a villain, and a new 
candidate seems to come forward every day. OER surveys the merits of the leading 
contenders. <br>
<br>
<b>Oil traders:</b> In times of high prices, OPEC has traditionally chosen to 
blame speculators, to deflect attention from its own policies. Often it's a 
fairly weak argument, but this time OPEC has a point: the cartel's 11 members 
are pumping almost every barrel they can in a bid to calm prices, but traders 
keep on bidding the crude price higher and higher. These extra barrels are 
proving ineffective in the face of aggressive futures trading. Oil traders and 
hedge funds are dominating today's market, and with OPEC spigots already open, 
there's little the cartel can do in the short term. <br>
<br>
<b>OPEC:</b> Criticisms of OPEC focus on the failings of its medium- and 
long-term policies, not its actions in recent weeks. A growing chorus of 
disapproval from analysts, trading floors and think-tanks argues that OPEC is to 
blame for failing to invest in extra capacity since 2000, despite having plenty 
of spare cash thanks to high oil revenues. That's why its hands are tied now 
that the world is crying out for more oil. &quot;OPEC has refrained from adding to 
its capacity since 2000, disregarding market signals such as growing demand and 
rising prices,&quot; says the London-based Centre for Global Energy Studies. Others 
take a similar view. &quot;They've completely underestimated the demand,&quot; said Bruce 
Evers, an analyst with investment bank Invsetec. &quot;Everything that could go wrong 
has gone wrong.&quot;<br>
<br>
<b>Vladimir Putin:</b> There is little doubt that the Yukos Oil court case is 
one of the key factors in the minds of the oil industry at the moment. Traders 
fear that Yukos may be forced into bankruptcy as a result of the court case 
brought by the Russian government for non-payment of taxes. This in turn poses a 
very real threat of stopping Yukos's oil output. With Yukos pumping some 1.8 
million barrels per day--some 2 per cent of global production--this could cause 
severe problems, and is one of the main reasons that oil futures are so 
expensive. <br>
<br>
That said, it is stretching the argument to lay the blame for today's high oil 
prices squarely at the feet of Putin. Some of his critics argue that the case 
against Yukos is a thinly veiled political attack on Yukos chairman Khodorkovsky, 
one of Putin's main political opponents. But it is quite a leap from personal 
squabble in the Kremlin to the Nymex trading floor in New York. <br>
<br>
<b>George W. Bush: </b>American motorists don't like paying top dollar to fill 
up their gas-guzzling sports utility vehicles and pick-ups. So when pump prices 
soar, as they are doing at the moment, they look for someone to blame. 
Increasingly, they are pointing the finger at President Bush. Why? Because 
America has a huge Strategic Oil Reserve kept in vast caves underneath some of 
its southern deserts. It keeps this oil for use in times of crisis, although 
what constitutes a crisis is thinly defined. So far, the Bush administration has 
resisted suggestions that the current situation constitutes a crisis. <br>
<br>
Others disagree, including influential US economist and Forbes magazine 
columnist, Professor Steve H. Hanke. He argues that President Bush jr. should 
follow the lead taken by his father when he was in the White House during the 
1991 Gulf War. Then, President Bush sr. released the Strategic Oil Reserve, and 
the price of a barrel of oil fell from $32.25 to $21.48 in one day. &quot;The lesson 
is clear,&quot; writes Hanke. &quot;We have an oil weapon, too. The strategic reserve 
should be used to bloody OPEC's nose, not prop up a cartel.&quot; <br>
<br>
So much for all the naming and shaming. The real question is: what's the likely 
direction for oil prices in the coming months and years? For what it's worth, 
most rational observers think that prices above $45/b are unsustainable – but 
that oil will not fall below $30/b any time soon. Demand is strong while supply 
is weak, and that, in a nutshell, is why prices are high. <br>
<br>
Francois Bourguignon, chief economist at The World Bank, is a welcome voice of 
reason among the cacophony of blame. On the supply side, he says producers will 
exploit wells that are not profitable at $30 a barrel, but might be at a higher 
price. The resultant supply increase will drive down prices. He says there are 
undoubtedly some objective causes for the price rises, such as China's strong 
growth and economic recovery in the United States and Japan. However, he said 
the price rise is also fuelled by speculation and uncertainties over the fate of 
the Yukos. (He discounts the crisis in Iraq because it had not been an important 
oil producer before the US-led occupation last year). On balance, he thinks 
prices will return to the low $30s by early 2005. &quot;I think we will return to a 
balanced price in a few months' time.&quot; </font></p>

