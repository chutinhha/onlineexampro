<br>
</font><b><font face="Verdana" size="1">Customers react to SMS marketing 
messages much more warmly than what they do to certain other means of marketing. 
Matt Harrison presents a case for mobile marketing</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im37.jpg" align="left" hspace="5" width="200" height="176">With 
the number of global mobile subscribers passing the 1.5-billion mark in the 
first week of June 2004--and research indicating we'll break the 2 billion 
barrier by as early as July 2006--and a second Oman GSM operator coming online 
very shortly, the issue of mobile marketing is becoming a hot topic. In fact, 
your mobile phone is no longer only a communication tool - it can be used for 
marketing, advertising, as a distribution channel, as well as an entertainment 
and business device. <br>
<br>
Without doubt, mobile marketing offers great potential. According to a recent 
University of Cambridge study, there has been a sharp increase in the number of 
users receiving SMS advertising. Globally, 40 per cent of users have received an 
SMS ad as of July 2003 on their mobile phones. Response rates to that 
advertising averages 3 per cent. At first glance, this may not look too 
impressive, but these numbers are encouraging and are in some cases better than 
initial response rates to direct mail marketing. <br>
<br>
<b>Give it to me straight </b><br>
But what exactly is mobile marketing? Put simply, it's the use of the mobile 
medium as a communications and entertainment channel between a brand and an 
end-user. It's the only personal channel enabling spontaneous, direct, 
interactive and/or targeted communications, any time, any place. Mobile 
marketers, particularly in the Far East, Europe and the US, are using the mobile 
phone in a wide variety of ways, examples include: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">For 
customer acquisition and retention. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">For 
loyalty building. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">As a 
sales promotion tool. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">To 
support product launches. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">To 
raise brand awareness. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">As a 
redemption / coupon mechanism. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">For 
direct marketing. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">As an 
effective B2B communications vehicle. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">As an 
additional revenue stream for news / information services. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">As a 
channel for delivering ring tones and logos. <br>
<br>
Put simply, SMS campaigns allow companies to zap incentives--for example, coupon 
codes, raffle numbers, or VIP passes--straight into the hands of customers. And 
although such campaigns have been used for several years by megabrands like 
Pepsi and MasterCard in Europe and Asia, they're still relatively new to the 
Gulf. <br>
<br>
However, with average click-through rates that are five times higher than 
standard online advertising and stronger recall rates than television 
advertising, mobile marketing is coming of age as the most productive and cost 
effective medium for reaching a highly desirable demographic. <br>
<br>
In fact, mobile marketing has proven its appeal across a wide variety of 
industries including automotive, travel, high tech and media. All of the top 
three US car manufacturers are strong supporters of mobile marketing and have 
deployed a variety of campaigns - from new customer acquisition initiatives to 
brand awareness campaigns that leverage games and activities to strengthen 
existing customer ties. <br>
<br>
<b>Great potential </b><br>
At a time when consumers hang up on telemarketers, delete spam, and toss direct 
mail into the bin, they remain surprisingly receptive to marketers texting them. 
A stunning 84 per cent of European mobile users are willing to receive SMS 
marketing and promotional offers on their handsets, yet the marketing community 
is failing to capitalise: just 18 per cent of Europe's marketing and advertising 
agencies currently offer mobile messaging to clients, and a worrying 57 per cent 
have no immediate plans to do so in the future.<br>
<br>
European mobile operators admit that less than 10 per cent of their current 
application (i.e., non person-to-person) traffic falls into the mobile marketing 
category. However, the majority are optimistic about future prospects, with 57 
per cent confident that mobile marketing will constitute a significant revenue 
stream, and 24 per cent claiming it already does. <br>
<br>
Increasing use of multi-media messaging (MMS) containing music video, TV or 
movie clips is seen as a key growth driver within the industry: an opportunity 
borne out by the massive 70 per cent of mobile users who claimed they would 
prefer to receive multi-media marketing content than a straight text message.
<br>
<br>
To support future growth of this sector, the majority of mobile operators are 
already thinking about the additional requirements for MMS: 25 per cent already 
have infrastructure in place and a further 55 per cent plan to do so; 5 per cent 
of these are already running a pilot. <br>
<br>
A further survey revealed that 65 per cent of participants said voice mail or 
direct phone contact was the most despised method of contact for marketers. The 
survey also found that approximately 56 per cent of respondents would prefer not 
to receive text messages from the companies they already have dealings with. The 
respondents who said that they would be receptive to various select services via 
SMS expressed interest in the following:<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Wireless 
bills/account updates: 55% <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Direct 
response surveys: 54% <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">New 
product availability updates: 38% <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Financial 
services (i.e., banking, stocks, insurance): 34% <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Sports-related 
scores and stories: 33% <br>
<br>
<b>SMS in action </b><br>
Let's consider a typical grassroots SMS campaign. Marketers at a 
Massachusetts-based shoppong mall had tried it all - mass mailings, print ads 
and radio spots. But these weren't bringing in the shoppers, and the mall was on 
the hunt for something truly attention-grabbing. So, last winter, the mall's 
marketing team turned to the mobile phone and collected the numbers of nearly 
2,000 shoppers. <br>
<br>
Over a six-month period, the customers received, via SMS, 39 discount offers 
from 19 tenants - messages like '25% off at the register' or 'Free drinks when 
you dine with a friend.' Shoppers simply presented their phones at the mall's 
information desk in exchange for paper coupons to redeem in stores. The result? 
An astonishing 80 per cent of all the SMS coupons were converted. The mall's 
tenants that participated all saw an increase in sales. <br>
<br>
<b>A bluffer's guide </b><br>
As can be seen from the Massachusetts shopping mall campaign, SMS marketing 
isn't a mysterious craft performed by techie whizkids hidden away in a dark room 
living off pizza. It's governed by the same principles that guide direct 
marketing and direct response advertising, courting consumers for their 
attention and entering into a respectful, relevant and engaging dialogue. <br>
<br>
What makes SMS marketing special are a number of unique features. It's 
unrivalled in its directness, the very personal nature of the interaction and 
its heavy contextual dependency. Indeed, here are ten all-important factors, 
which should to be taken into account when running an SMS campaign. <br>
1) To kick off with, establish whether SMS is a relevant channel of 
communication for your product or service and your audience. <br>
2) Choose a reliable SMS supplier. Whichever company you choose to work with, 
ensure they can support both the creative and technical aspects of wireless 
marketing campaigns in-house and that they have the capabilities to design, 
implement and track campaigns.<br>
3) Decide whether to recruit campaign participants through off-line media, such 
as print, radio or TV or to use an opt-in database. Unless you already have an 
opt-in database, your other media activities and time restrictions will be the 
deciding factors between these two options. <br>
4) A successful dialogue, in any situation, is based on the willingness of both 
parties to engage in it. The same stands true for SMS. Permission is key and 
consumers have to feel that they are in control of the dialogue at any point in 
time. Mobile phones are very personal devices and a mobile phone number can 
either be a long-term asset or a quick route to a potentially angry customer.<br>
5) If you're looking to buy a third party database, ensure that the users are 
not just opted-in, but properly managed members. Important questions to ask the 
database owner include, what have users opted in to? How easily can they opt 
out? How often do they get information? How relevant is it for them? <br>
6) When designing the campaign, ensure you take advantage of the strengths of 
the medium. And, whatever you're proposing to consumers, it has to be fun, 
engaging and relevant. <br>
7) Interactivity is also one of the key challenges. Rather like Pandora's Box, 
if you choose to open this channel of communication to your customers, it isn't 
something you can then close. What you discover from receiving feedback and 
questions from your customers will be revealing, and you certainly have to be 
ready to continue the dialogue on a one-to-one basis. <br>
8) Test and learn: SMS allows immediate tracking, so you can test the response 
rates of different channels of communication and different mechanics. Be ready 
to adapt in real time. <br>
9) The end of the campaign is just the beginning of a long-term relationship. 
Don't just leave it there. <br>
10) Finally, give your campaign participants a reason to be your ambassadors. If 
your content is relevant and exclusive, your customers will share messages with 
friends and family, creating a viral effect. Imaginative and value adding SMS 
campaigns can reward the best ambassadors. <br>
<br>
<b>That's it folks! </b><br>
The bottom line is that there are a number of mobile marketing trends Oman-based 
marketers ought to be thinking about: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Customers 
are aware of the potential of new media mobile services. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">There 
is intent to purchase things that have a perceived benefit. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Consumers 
are interested in making purchases with the phone. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Mobile 
advertising and marketing has huge potential. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">Further 
segmentation and research on gender, age and lifestyle are required. <br>
<br>
The potential for SMS will continue to grow as more phones are developed and 
sold with advanced technologies that allow for this type of marketing. 
Therefore, the shift from wire line communications to wireless presents a myriad 
of opportunities to marketers, including the dissemination of photos, picture 
brochures and other enriched collateral. In the end, the companies that best 
adapt the old-world approach to selling and understanding their customers will 
be the clear winners in marketing via this new channel.<br>
