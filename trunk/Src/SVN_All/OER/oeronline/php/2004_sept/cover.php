            TURMOIL!<br>
            Storm in an Oil Barrel<br>
            </font></font><font face="Verdana" size="1"><b>
            <br>
            Anyone who expected that the invasion and occupation of Iraq would 
            lead to a sharp decline in the price of oil has been sorely 
            disappointed. Instead of falling, oil prices have continued their 
            record-breaking run and busted through the $49-mark on August 20, 
            the last day of the September futures contract, amid concerns that 
            demand will outstrip supply. OPEC president Purnomo Yusgiantoro 
            maintained in a statement earlier in the month that the oil 
            producers’ organisation was unable to pump more oil and thus bring 
            down the price. Vicky Kapur provides an overview of the world oil 
            situation, analyses the reasons behind the current spike in prices, 
            reckons the overall impact on the GCC economies, and hazards a guess 
            as to whether expensive oil is here to stay</b></font></p>
            <p align="justify">















































            <img border="0" src="cover2.jpg" width="250" height="185" align="left"><font face="Verdana" size="1">Oil prices climbed nearer to
the long envisaged $50 a barrel-
mark last month before
retreating as traders reacted
to reports on the conflict in Iraq and the
prevailing uncertainty in Russia, concerned
that growing ambiguity might interrupt
crude oil exports. On the New York
Mercantile Exchange (Nymex), crude oil
prices rose as high as $49.40/b before settling
at $47.86/b on August 20, the last
day of the September futures contract.<br>
            <br>
It was less than three months ago that
the prices broke the $40/b boundary amid
regional tension and attacks on the oil
pipelines in Iraq. However, the promise
by Organisation of Petroleum Exporting
Countries (OPEC) to increase production
helped cool down the feverish market and
prices dipped below the $40 plateau. Adjusted
for inflation, world oil prices remain
far below the levels reached during the
1970s oil shock, but have shot up dramatically
from the beginning of 2002, when
the price per barrel hovered around $18/b.
<br>
            <br>
            With predictions of the cost of a barrel
of crude topping $50 almost coming true, some economists are predicting that global
stagflation is a real possibility. It is,
indeed, if the prices stay at this level or
even higher in the months to follow.
There are a lot of questions that come to
mind while analysing the situation. For
starters, what on earth has happened for
the prices to have shot through the roof?
The simple economics of demand and
supply would suggest that there is a
shortage of oil in the market, thus pushing
the price ever higher.
<br>
            <br>
            Peculiarly, however, the simple laws
of economics do not govern oil prices. To
be sure, the supply of oil far exceeds the
current demand for oil in the market. It
is, in fact, fears about disruptions to the
world's oil supply that are to blame for
the recent high oil prices with record high
being hit last month. Soaring demand
both from the US and in particular from
China's fast growing economy combined
with security fears have been driving
prices higher for some months.
<br>
            <br>
            <img border="0" src="im25.jpg" align="left" width="200" height="125">A main worry is that oil production
is close to its limit and any disruption to
supply would prove difficult to smooth
out. At the same time, an economic
recovery is boosting the world's thirst
for oil. Emerging economies are proving
the most difficult to satisfy, with
Chinese imports of crude oil jumping
40 per cent in the first seven months of
2004 from a year earlier.
<br>
            <br>
            In a recent interview to BBC Talking
Point, Dr Adnan Shihab-Eldin, OPEC's
director of research, said: ''Oil prices
shouldn't be at these high levels in our
view because, currently, the supply available
in the markets far exceeds the demand.
The global increase in prices of crude
is mainly on the concerns of market speculators
that there will not be enough
supply should there be a supply disruption.
They are not concerned about the
current supply. They are concerned that
should there be a major supply disruption
in the future, there may not be enough
supplies coming from other countries.
I can assure you that today OPEC
is putting in the market close to 30 million
barrels a day, far ahead of its ceiling.
And, as a result, we have more than 2
million bpd excess supply over demand.
So, it's basically a question of fear.''
<br>
            <br>
            <b>Adding fuel to fire
</b><br>
            Investors have become increasingly
nervous in recent weeks and have
identified a number of trouble spots that
could provide an unwelcome boost to
oil prices. Many believe the recent price
hike should be branded with a 'Made in
Russia' label. Russia's largest oil producer, Yukos, is in chaos and could stop
production. Yukos produces one-fifth of
Russia's oil output-or about 2 per cent
of global production-and is locked in an
ongoing tug of war with Russian authorities
over a $3.4 billion (RO1.31 billion)
tax bill, fraud charges, and the fate of its
ex-CEO, the billionaire oligarch Mikhail
Khodorkovsky, who is now in jail.
<br>
            <br>
            <img border="0" src="im26.jpg" align="right" width="200" height="126">However, a closer look reveals that
there is a long list of factors contributing
to the price increase. According to analysts,
the fear premium-the increase in
prices resulting from political tension in
main oil producing countries like Saudi
Arabia and Iraq-adds $6 to </font><font face="Verdana" size="1">$8 to the
price. This coincides with the uncertainty
accompanying the presidential referendum in Venezuela, the world's
fifth largest oil producer.<br>
            <br>
            Oil markets have been on
the edge in recent weeks, their
concerns heightened by potential
trouble in many of the
world's largest oil regions. In
Russia, it was long-running
saga between the Russian government
and oil giant Yukos;
in Venezuela, it was a contested
referendum on whether to oust President
Hugo Chávez; in the Gulf of
Mexico, it was the passage of Hurricane
Charley, forcing the temporary closing
of oil installations; and in Iraq, it was
Shia cleric Moqtada al-Sadr and his followers,
who refer to themselves as the
Mahdi Army. All this and more are providing
traders with plenty of jumpy
moments. Any significant capacity is
seen as unreliable - Iraq, Russia and
Venezuela, for instance.<br>
            <br>
In Iraq, supporters of al-Sadr issued
threats of sabotage against the country's
oil pipelines. Oil exports were cut in half
to 1 million barrels per day (bpd) on
August 8 after fighting forced the closing
of one of two pipelines in the south
of the country. The other main outlet for
Iraq's oil production, a pipeline linking
the country's northern oil fields to Turkey,
has been bombed repeatedly in the
last year, forcing its partial shutdown.
Iraq's oil production before the war
hovered around 2.2 million bpd.
<br>
            <br>
            <img border="0" src="im27.jpg" align="left" width="200" height="125">While the pressure on Yukos seemed
to have eased out by late August and
Chávez won the referendum in Venezuela,
the situation in Iraq is still unpredictable
although oil supply from its
southern oilfields had been restored by
end-August. Since most countries are
producing at their maximum levels, an
interruption in exports from Iraq or elsewhere
would be hard to offset. OPEC
accounts for about a third of the world's
oil production, but has little spare capacity
to make up for production shortfalls.
OPEC's 11 members are pumping
30 million bpd, the most since 1979.
They can lift production to 30.5 million
bpd in September, according to OPEC.
<br>
            <br>
            With uncertainty likely to continue,
OPEC likely to keep brushing up against
its capacity limits, and Yukos certain to
remain under fire, the world may just
have to get used to expensive oil.
<br>
            <br>
            <b>The thirst for oil
</b><br>
            Chinese consumption has increased
global demand over the last year.
Both the Paris-based International Energy
Agency (IEA) and Vienna-based OPEC
have been caught short by the strength
of global oil demand. Both raised their
forecasts in recent days. The IEA has
revised upwards its estimate of world
oil demand, quashing hopes of an imminent
decline in oil prices. The IEA projects
global oil demand at 82.2 million
b/d this year, about 750,000 barrels
more than previously forecast. For its
part, OPEC now expects global oil consumption
this year to reach 81.18 million
b/d, up 280,000 barrels from an
earlier estimate.
<br>
            <br>
            <img border="0" src="im28.jpg" align="right" width="200" height="123">The IEA added that the effective
spare capacity of OPEC had
narrowed to about 600,000
b/d in July, following recent
efforts to keep pace with rising
demand. The IEA, set
up 30 years ago to advise 26
industrialised oil consuming
nations, said world demand
would rise by a further 1.8
million barrels next year to
84 million b/d. It added that
OPEC's production capacity
would rise by 400,000
b/d this year, and by a further
700,000 b/d in 2005.
<br>
            <br>
            ''The market is tight,
production and infrastructure
capacity is less than
desired, and uncertainties
continue to weigh on the
market,'' the agency said.
On a positive note, the IEA added that it did not expect Russian supplies to be 
            disrupted despite w</font><font face="Verdana" size="1">idespread concerns over the future of local oil giant
Yukos, responsible for one-fifth of the
country's output. Worries that Yukos
could be forced to suspend production
because of a tax dispute with the government
have contributed to the recent
surge in oil prices.
<br>
            <br>
            <b>Pumping all they can
</b><br>
            Not only has Iraq's oil industry, hampered
by creaking infrastructure
and sabotage, been unable to pump anything
like its potential capacity, but supply
elsewhere is under pressure too. Oil
traders remain nervous about the capriciousness
of the Russian state. They are
also worried by the apparent impotence
of the OPEC. On August 3, 2004, HE Purnomo Yusgiantoro, OPEC president
and Indonesia's oil minister, stunned observers
by saying that the organisation
would be unable to pump any more oil
to alleviate the pressure on prices. ''There
is no additional supply,'' he said.
<br>
            <br>
            <img border="0" src="im29.jpg" align="left" width="156" height="123">Like the oil market, the money markets
are fretting that the high price could
hurt the world economy. Yusgiantoro's
comments sparked a sell-off on Western
stockmarkets. OPEC's power has been
on the wane since the oil crises of the
1970s, when the organisation was able to
triple prices almost overnight by restricting
supply to Western consumers. Since
then, industrialised economies have reduced
their dependence on oil, but it is
still a crucial commodity, and OPEC still
accounts for 40 per cent of world oil
production. Moreover, Saudi Arabia,
OPEC's leading member, alone accounts
for a quarter of the world's proven oil reserves.
It has been able to influence prices
simply by turning its taps on and off.
<br>
            <br>
            In the recent past, it has been OPEC's
ability to turn the taps off that has been
in doubt. Members tended to exceed the
production quotas set at the organisation's
regular meetings, in the hope that
other members, especially Saudi Arabia,
would keep to their limits, and thus
support the price. The incentive to cheat
grew even stronger as non-OPEC suppliers,
especially Russia, grew in importance.
(Russia's output has increased by
2 million bpd every year for the past three
years.) OPEC's current production of 30
million bpd (including Iraq) is well above
its official quota of 26 million bpd.
<br>
            <br>
            <img border="0" src="im30.jpg" align="right" width="143" height="122">But now it is OPEC's ability to open
the taps further that is in doubt - and at
a time when the antics of Russian prosecutors
are also raising questions about
supply from that country. OPEC's spare
capacity is now thought to be 1-2 per
cent of global demand, well under the 4
per cent that is thought necessary in
order to influence prices. That gap cannot
be closed quickly since the oil-production
business has long lead times and
any new oil will take a couple of years
at least to come on stream.
<br>
            <br>
            Only Saudi Arabia, the world's biggest
oil producer and OPEC's most powerful
member, can still increase its output.
The country has already increased
production by 1 million b/d in recent
months and is currently pumping 9.25
million b/d (July output), well above its
8.45 million quota, but below its 10.5 million
official capacity. Ali al-Naimi, the
Saudi oil minister, said the country could
bring another 1.3 million b/d to the market
if needed. Saudi Arabia is close to
opening two new production plants at
Abu Safah and Qatif, which will pump
close to 1 million b/d. However, these
are meant to replace production from
older facilities, not to add new output.
<br>
            <br>
            <img border="0" src="im31.jpg" align="left" width="200" height="130">In addition to OPEC's attempts to
increase supply, the IEA also expects
non-members to boost output by a combined
1.2 million bpd over the coming
year, of which around half will be from
the former Soviet Union. But that figure
is hostage to events in Russia. At 1.7
million bpd (more than is pumped from
all of Libya's wells), Yukos' output
makes up 2 per cent of global oil production
and thus has a noticeable effect
on the price. With the Russian officials'
attempts to sell the company's prize
subsidiary, Yukanskneftegaz, thwarted
by the local judiciary, oil traders are
likely to remain jittery.&nbsp;
&nbsp;<br>
            <br>
            <b>The price of costly oil
</b><br>
            Last month, IEA chief economist Dr
Fatih Birol told BBC News Online
that economic growth would slow unless
oil prices retreated from their current
highs. OPEC characteristically tried to
play down the threat posed by high oil
prices, maintaining that it had boosted
production in July by 599,000 barrels to
29.57 million b/d. ''The direct contribution
of the concern-driven rise in oil
prices to the economic slowdown in
2004 has been very small,'' the organisation
said in its monthly statement.<br>
            <br>
However, it also warned that high
oil prices could cause problems next
year. ''Going forward, much will depend
on how long the price of the
OPEC reference basket remains
above $35,'' the group said. ''The
effect would be greater in 2005. The impact
on the developing world would be
greatest in the large trading economies
of South East Asia.''<br>
            <br>
There are good reasons to suppose
that the world will have to get used to a
high oil price for a good many years yet.
Why might this be so? And what might
it mean for the price of financial assets?
OPEC has put the blame for the rising
oil price largely on speculative excess.
According to this argument, large purchases
by hedge funds, those free-wheeling
pariahs of international finance, have
been responsible for pushing up the price.
<br>
            <br>
            When the hedge funds cut and run,
the argument goes, the oil price will fall.
However, the price has continued to rise
in early August even as those positions
were being significantly reduced. In any
case, long-term consumers clearly do not
believe that the oil price will fall much.
When the spot price (i.e., for immediate
delivery) has soared in the past, the forward
price (for delivery in the future)
has barely budged, because consumers</font><font face="Verdana" size="1">

expected the price to fall again: in October
2000, when the spot price reached
$38, the forward price stayed at $20. This
time, the forward price has climbed sharply
too: the price of oil for delivery in
ten years' time has reached $35 a barrel.
<br>
            <br>
            Perhaps buyers are willing to pay this
apparently heady price because it is, it
transpires, not so elevated after all. Since
January 2000, the average price of oil has
been about $30, points out Jeffrey Currie,
head of commodity research at Goldman
Sachs. The only time in that period that
it has fallen below that price for any
length of time was immediately after the
terrorist attacks on September 11, 2001.
<br>
            <br>
            An unexpected increase in demand
from a growing world economy, in particular
from China, has helped push the
oil price up. So have growing worries
about supply. But these supply worries
reflect deeper problems of under-investment,
argues Mr Currie. There has been
no growth in pumping and refining
capacity since the 1970s; all the growth
in output of the past three decades has
come from squeezing more oil out of
existing fields. Last year, growth in demand
outstripped growth in refining capacity
by 15:1. The rise in the oil price is
both a reflection of past under-investment
and, of course, a spur to future
investment. It will, however, need oil to
stay above $30 a barrel for several years
to solve these supply problems.
<br>
            <br>
            What a high and rising oil price might
mean for the world economy is the subject
of much debate among economists.
The sanguine point out that
the price is still considerably
lower in real terms than it was
when it hit giddy heights in
the 1970s. And rich countries
are, moreover, less dependent
on the stuff than they used to
be. However, the more nervous
among them worry about
the impact that such high
prices will have on the global
economy as a whole.
&nbsp;&nbsp;<br>
            <br>
            <b>Oil oil everywhere…
<br>
            </b>Oil is in the 'breaking news' section
of the world media again. Never
far from newsworthy, such is its overriding
economic and political importance,
the spotlight has returned as
harsh as ever. The price of benchmark
US light sweet crude recently reached
its highest level for 13 years, and the organisation of petroleum exporting
nations, OPEC, maintained its inability
to raise production by more than 1 million
bpd for another year.<br>
            <br>
Add increasing violence in Iraq
delaying its return to full oil production,
and it is understandable that oil analysts
have been getting the jitters. And oil
giant Shell already admitting twice this
year that it has over-estimated its own
oil reserves. But looking behind the politics
and other reasons for the upward
pressure on prices (such as historically
low oil and petrol stockpiles
in the US), just how much oil
is left under the surface?
<br>
            <br>
            Loads - or more than 1,000
billion barrels of proven reserves
to be precise, up to 40 years'
worth. Add that still yet to be
discovered, and the figure and
timescale is much higher still.
Yet, there are two small cautioning
factors. Firstly, that the
world is consuming oil as fast
as it can get its hands on it (75
million bpd rising to 120 million
bpd by 2030 according to
the IEA); and secondly, will it
be economically viable or technically
possible to get our
hands on all the remaining oil?
<br>
            <br>
            However, one thing is for
certain - oil will run out eventually.
If you stand back and
look at it totally objectively, it
took millions of years to develop
each drop of oil, which
we now use in seconds, so it is
100 per cent certain that oil
will one day run out. However,
we have potentially got
another 100 years of oil production.
Yet, as it becomes
more scarce, prices will no
doubt go up, and there will
inevitably have to be a gradual move to
alternative power sources.
<br>
            <br>
            <b>Looking down the barrel
</b><br>
            Trying to determine how many more
decades of oil the world can enjoy is
like asking the question 'how long is a
piece of string? Technology has improved
so much over the last 10 to 15 years
that countries and companies can now
get at oil reserves that previously would
have been considered impossible. And
over the next decade or so, even better
technologies will no doubt come on
stream. For example, engineers can now
take 3D or even 4D seismic images to
stretch existing reserves and aid the
finding of new ones. Obviously, the
future depends on continuing demand
for oil, but there are a number of newly
developing fields around the world -
such as Azerbaijan, Russia and Angola.<br>
            <br>
            World oil prices continue their mar</font><font face="Verdana" size="1">ch 
            &nbsp;upwards, shrugging off a warning by
the IEA that the market is showing &quot;irrational
exuberance&quot;. The IEA began its
monthly review of global energy markets
with a chapter headlined &quot;Irrational
Exuberance&quot;, echoing prophetic
words used by US Federal Reserve chairman
Alan Greenspan well before the
stock market bubble burst 3 years ago.
<br>
            <br>
            The market remains on edge over
any potential disruption to supplies,
which are running at almost full-throttle
to satisfy the fastest growth in demand
for over two decades. Still, according
to an annual survey by Oil &amp; Gas
Journal, worldwide exploration and production
spending is expected to rise 4.8
per cent this year to $53 billion (RO20.38
billion). But that does not mean oil and
natural gas markets will necessarily be
awash in new supplies anytime soon.
Analysts say that it will take between
12 and 18 months for the investments of
both OPEC and non-OPEC members to
bear fruit and relieve the market.<br>
            <br>
Also, the IEA expects oil demand to
grow by 3.2 per cent in 2004 to 81.4 million
b/d, leaving very little spare production
capacity. The global demand is
growing at the fastest rate since the late
1970s. To meet the skyrocketing demand,
the supply side is being pushed to
the limit. OPEC is already pumping at
close to capacity. Still, analysts agree
that the main players in the industry
are taking a relatively conservative
approach to boosting output. They cite
a number of reasons for the industry's
cautiousness, including the possibility
that a sudden economic recession
or a cool down in political
problems could shave
demand, tighten prices and
make any expansion in
drilling activities now economically
detrimental later.
<br>
            <br>
            Claude Mandil, executive
director of the IEA, said
oil prices could remain high
and volatile for the foreseeable
future. ''The end of 2004
may be critical,'' he said.
''There is less flexibility than we should
have, and than we had last year. It will
be more difficult to react to unpredicted
events,'' he said.<br>
            <br>
            <b>A welcome windfall?
</b><br>
            With oil prices hitting record highs
on a daily basis, economies in the
Gulf region are witnessing an incredible
boom that is affecting everything from
real estate to corporate profits. According
to a Standard Chartered report, the surge
in oil prices is ensuring record revenues
and the combined net oil exports of the
six Gulf Cooperation Council (GCC)
countries are on course to exceed $180
billion (RO69.23 billion) in 2004, leaving
the region in economic boom.
<br>
            <br>
            Record oil prices are leading to record
government revenues, and the projected
figure is $35 billion (RO13.46 billion)
more than 2003, when oil prices
were perceived to be already strong.
While Iraq and regional security concerns
may dominate the international
agenda, the local focus is
resolutely on the current
economic boom. And high
oil prices underpin that
boom, with supply concerns
and strong global
demand pushing oil prices
to 20-year highs. To
match demand, the Gulf
is now close to producing
oil at full capacity, and oil
process are expected to remain high and
volatile, with OPEC's benchmark oil price
forecast to average $35 in 2004.
<br>
            <br>
            The Saudi National Commercial
Bank said in a report last month that
Saudi was set to achieve a budget surplus
of SR56 billion ($14.9 billion) in
2004 on the back of high oil prices. ''The
forecast budget deficit of SR30 billion
($8 billion) will be offset by a surplus of
SR56 billion.'' Saudi Arabia, in particular,
has been the primary beneficiary of
this trend, thanks to the sheer volume
of its production capacity.<br>
            <br>
            Of course, no one believes oil prices to stay in the stratosphere. 
            Even Crown Prince Abdullah called for a price in the

$25-$30 range. The question then is how
long this windfall will last and what
effect 'back-to-normal' prices will have,
if any, on future corporate earnings?
<br>
            <br>
            A report issued in by a specialised
magazine in the US stated that it expected AGCC countries as a group to achieve,
during the current year, enormous gains
in the light of the continuous oil price
hike, and estimated that the financial
surplus of their budgets in current year
might reach $70 billion (RO27 billion) at
17 per cent of their total local products.
<br>
            <br>
            The report estimated that the volume
of oil revenues in the UAE would reach
$30 billion (RO11.54 billion) this year
compared to Saudi Arabia's $102 billion
(RO39.23 billion), which increased its oil
production by 9.1 million bpd, and Kuwait's
$25 billion (RO9.62 billion).
<br>
            <br>
            Strong oil prices pumped an additional
$33 billion (RO12.7 billion) into
the coffers of the GCC in 2003. The six
Gulf nations netted around $133.4 billion
(RO51.31 billion) last year compared
with nearly $100.6 billion
(RO38.7 billion) in 2002, according to
the London-based Centre for Global
Energy Studies (CGES), which is
owned by former Saudi Oil Minister
Sheikh Ahmed Zaki Al Yamani.
<br>
            <br>
            That improvement was a result of a
surge in the price of OPEC's basket of
seven crudes to around $28.1 a barrel
in 2003 from $24.5 in 2002. Today, that
price hovers above $45 a barrel, and
could stay there for some time to come.
What that means for the oil economies
of GCC is anybody's guess.</font></p>
