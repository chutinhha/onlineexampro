                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Change of tact</font><p><font face="Verdana" size="1"><b>With ambush marketing looming large over international sporting events, sponsors and organisers are struggling to protect themselves from guerilla assaults, says Rumali Chakravarty</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_november/ambus-1.jpg" align="left" hspace="5" vspace="5" width="225" height="176"></font><font face="Verdana" size="2">Paying millions of dollars as sponsorship fee to international sporting events no longer ensures that your brand will get the maximum mileage. From Atlanta to Sydney Olympics, and World Cup football in Japan and Korea to Winter Olympics in Norway , countless international and national tournaments have seen genuine sponsors left holding the ball, while their competitors who didn't commit a penny walked away with the prize of brand recall.<br>
                    This is called "ambush" or "guerilla" marketing. It denotes the actions of companies who cleverly connect themselves with a big event without paying sponsorship fees. An ambush marketer thus derives some, if not most, of the benefits of a legitimate, paid-for sponsorship, at relatively low cost. Small wonder then, guerrilla techniques are at their peak when the stakes are highest. And galactic events such as the Olympics, and World Cup football or cricket, are occasions where marketing marauders rule the roost.<br>
                    The danger, of course, is that such marketing can cause confusion among consumers, or otherwise impact negatively on the sponsorship rights. It can undermine an event's integrity, embroiling sports people in the murky world of marketing feints and ripostes. And, most importantly, it can degrade the abilities of the organisers to attract future sponsors. It can also violate the law.<br>
                    <br>
                    <b>The ICC case</b><br>
                    Ambush marketing came to the fore recently, when the International Cricket Council (ICC) succeeded in forcing players to sign a controversial contract. They agreed not to endorse individual products or companies during the ICC Champions Trophy in Sri Lanka in September 2002. The world was witness to the fervent negotiations between the apex international cricketing body, the national cricket boards and the players; many of whom perceived that the contract would infringe upon their own rights of sponsorship. ICC had signed up heavyweight sponsors for this major tournament and the next World Cup, and was determined to protect them from getting ambushed by opportunistic rivals.<br>
                    <br>
                    <b>1994 Winter Olympics at Lillehammer.</b><br>
                    In the 1994 Winter Olympics at Lillehammer in Norway, credit card giant Visa was official sponsor. Visa played up on the line that American Express cards would not be accepted at the Olympic Village. American Express pulled a hijack, with a campaign based on the slogan "If you are travelling to Lillehammer, you will need a passport, but you don't need a Visa!" This was factually correct because Americans did not need visas to go to Norway.<br>
                    <br>
                    <b>Pepsi vs. Coke</b><br>
                    The chronicle of arch rivalry between cold drink giants Coca Cola and Pepsi is replete with examples of ambush marketing. FIFA, football's global governing body, took offence to an advertising campaign by Pepsi in Argentina. In the outcome, an Argentine court banned Pepsi's TV and press ads that suggested a "presumed sponsorship relationship". In fact, it was Coca Cola that was the official sponsor for the event. The offending ads had the words "Tokyo 2002" alongside famous footballers and other soccer imagery, and these were associated with the Pepsi logo.<br>
                    Patrick Magyar, the chief executive of FIFA Marketing, had said, "FIFA regrets that Pepsi employs these below-the-belt tactics that harm the FIFA World Cup - they should know better."<br>
                    The battle in Argentina was just one among several that Magyar fought in South America. But Pepsi still dominated television with the help of their brand ambassador David Beckham. In India, a poll of 8,000 Internet users done by AC Nielsen after the 2002 World Cup revealed that 13.1 per cent of the respondents recognised Pepsi as an official sponsor.<br>
                    Who can forget the 1996 cricket World Cup when Coke paid an emperor's ransom for the right to call itself the "official" sponsor. Rival Pepsi promptly waylaid Coke with a most memorable blitz based on the catch-line "Nothing official about it".&nbsp;<br>
                    <br>
                    <b>MasterCard vs. Sprint</b><br>
                    One of the few cases where legal remedy to ambush marketing has been provided, pertains to the 1994 football World Cup in the US. MasterCard had paid for the exclusive rights before and during the competition to use World Cup logos on "all card-based payment and account access devices". Sprint Communications was also involved in the World Cup as an official partner, a category that was neither as extensive nor as costly as that of a sponsor.<br>
                    Sprint's competence being in the field of long distance calls, they started selling pre-paid telephone calling cards in the US and Europe bearing World Cup logos. MasterCard took Sprint to the federal court in New York City, and established that Sprint's use of World Cup logos infringed MasterCard's category of "card-based payment and account access devices", even though the telephone cards were functionally not the same as MasterCard's cards.&nbsp;<br>
                    The court held that consumers, on seeing the Sprint card bearing a World Cup logo, would mistakenly assume that Sprint had rights in a category that, in fact, belonged exclusively to MasterCard.<br>
                    <br>
                    <b>Nike vs. Adidas</b><br>
                    Some of the most infamous ambushes in the last decade involved sports-goods companies. At the 1992 Barcelona Olympics, Nike sponsored a media conference of the US basketball team despite Reebok being the official sponsor for the games. Nike's man Michael Jordan, while accepting the gold medal for basketball, covered up the Reebok logo on his kit.<br>
                    At the next Olympics at Atlanta, Nike struck again by handing out flags to the spectators. Millions of viewers saw the Nike trademark swooshing about in the excitement of the games. Nike also opened a large complex next door to the main Atlanta stadium. A survey after the Olympics showed that more than 60 per cent of respondents believed Nike was an official sponsor, thus upstaging Reebok, which had paid US$40 million to be the official sponsor.<br>
                    Nike then ambushed Adidas, the official sponsor, at the 1998 football World Cup in France. Nike cashed in on the soccer mania with a huge advertising campaign that predominantly made use of its sponsorship of the Brazilian team.<br>
                    In return, Adidas 'thorpedoed' Nike at the 2000 Sydney Olympics. Nike was the official clothing supplier while Adidas sponsored the swimming super-hero, Ian Thorpe. At medal presentation, Thorpe (accidentally?) draped his towel over the Nike logo on his official team tracksuit. Thus Nike was nowhere in the famous photograph that was seen by multitudes of people in Australia and all over the world.<br>
                    Nike struck back at the 2002 Boston Marathon, a mega event by any reckoning. As the Adidas sponsored runners came off the course under full glare of the cameras, they were treated to spray painted 'swooshes' that honoured the day of the event but did not mention the race itself.<br>
                    As ambush marketing becomes more and more widespread - and acceptable - the losers could be the events themselves. Organisations such as the IOC, FIFA and ICC depend upon corporate sponsorships for survival. If brand marketers were to view official sponsorships as the equivalent of flushing wads of heavy denomination currency down a featureless black hole, then organisers will become strapped for the means to stage these events. But then, one man's ambush is another's good business. And you thought all the games were played by athletes!&nbsp;</font></p>
