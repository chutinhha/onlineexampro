                    <font face="Verdana" size="4" color="#FF0000">GOING SOLO</font>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_november/saud_logo.jpg" align="left" hspace="5" vspace="5" width="150" height="82">The Group's dynamic new symbol takes inspiration from Oman's rich heritage. The Dhow not only symbolises Omani seafaring traditions, but also the trading roots of the company. The modern rendition reflects the Group's ability to keep ahead of changing times. The multiple sails blooming display the zeal and the enthusiasm of the Group emphasising its strong focus on diversification and the readiness to seek new horizons and successfully conquer new challenges. The oval conveys a stamp of excellence.&nbsp;<br>
                    <br>
                    <b>
                    The Saud Bahwan Group&nbsp;</b><br>
                    <img border="0" src="../../images/2002_november/cover-saud.jpg" align="right" width="200" height="146">
 Today, there are seven companies under the Saud Bahwan Group (formerly Bahwan Automotive Centre). The new name for the flagship automotive division is Saud Bahwan Automotive LLC. The company employs over 1,000 Omani staff in fields such as sales and marketing, service and maintenance, customer care, advertising, accounts, audit, installment credit, administration, warehousing and computer services. The Group's widely diversified business activities are transportation, construction, agriculture, oil and gas projects, air and seaport equipment and systems, defence equipment and civic services and travel and tourism. Another new entity formed as a result of the recent disbandment is Saud Bahwan Projects &amp; Equipment LLC which will focus on construction equipment, oilfield services and transport refrigeration among other activities. The other companies in the Group include Saud Bahwan Trading LLC, Arabian Car Marketing Co. LLC, Arabian Engineering Services LLC, National Travel and Tourism, and Oman Tourism and Travel Agencies. Sheikh Saud is also a significant investor in other Omani enterprises.&nbsp;</font></p>
                    <p><font size="2"><b><u><font color="#ff0000" face="Verdana">Interview</font></u><font face="Verdana"><br>
                    <a name="Sheikh Saud Bahwan">Sheikh Saud Bahwan</a>&nbsp;</font></b><font face="Verdana"><br>
                    Sheikh Saud Salim Bahwan, Chairman of the Saud Bahwan Group,
                    began his career at the age of nine. From the time his
                    family used a wooden Dhow for transporting goods to far-off
                    places in his earlier days, Sheikh Saud now stands at the
                    helm of one of Oman's largest business groups which
                    represents world class brands such as Toyota, Daihatsu,
                    Lexus, Ford, Yokohama, Kubota, Komatsu, Kato and MAN.
                    </font></font></p>
                    <p><font face="Verdana" size="2">Sheikh Saud has won several
                    international accolades such as 'The Order of the Rising
                    Sun', the highest civilian honour bestowed by the Emperor of
                    Japan, and the region's prestigious award, 'The Gulf
                    Businessman of the Year'. He also received twice the 'Best
                    Management' award, given by the Chairman of Toyota Motor
                    Corporation, among others. OER met up with him to discuss
                    past events, recent successes and future challenges.&nbsp;<br>
                    <br>
                    <b>OER: </b>Could you tell us how you and your brother began
                    your business?&nbsp;<br>
                    <b>Sheikh Saud Bahwan:</b> My business association with my
                    brother goes back a long way. In our childhood days, we used
                    to trade by using dhows. Along with our father, we used to
                    charter the dhow, and carry goods and passengers to various
                    shores. Later, as part of our mother's wishes, my brother
                    and I became business partners and thus started the Bahwan
                    Group.&nbsp;<br>
                    <br>
                    <b>OER:</b> Could you throw light on the amicable settlement
                    reached between you and your brother?<br>
                    <b>Sheikh Saud Bahwan:</b> The two business Groups, Bahwan
                    Automotive Centre and Bahwan Business Centre, were
                    independently managed entities though the financial
                    ownership used to be equally divided between my brother and
                    I. According to the new agreement, all the companies and
                    activities formerly under Bahwan Automotive Centre will now
                    be 100 per cent owned by me, and similarly those under
                    Bahwan Business Centre will be owned by my brother. In
                    short, two distinct and totally independent business houses
                    have now come into being.&nbsp;<br>
                    <br>
                    <b>OER:</b> What are the challenges and opportunities ahead
                    for each entity?<br>
                    <b>Sheikh Saud Bahwan: </b>My group will work in line with
                    national priorities and look for synergies in our core
                    competency areas. We have always been open to exploring new
                    avenues for growth, and this will continue. We will
                    constantly look for associating ourselves with a diverse
                    range of activities with a commitment to excel by providing
                    world-class products and services.&nbsp;<br>
                    <br>
                    <b>OER:</b> What kind of future you envisage for your each
                    core business?<br>
                    <b>Sheikh Saud Bahwan: </b>Our group companies play a vital
                    role in a wide and diverse range of sectors -
                    transportation, agriculture, construction, defense, oil
                    production, airport and sea port infrastructure, civic
                    services, projects and travel, among others. In each of
                    these fields we have been able to gain customer confidence
                    and trust, and carve out a strong leadership position.<br>
                    In the automotive sector, our brands Toyota, Daihatsu, Lexus
                    and Ford are household names. Toyota has in fact set
                    standards for the automotive sector in Oman. The strong
                    customer base, cutting across various segments that we have
                    built over the years, is a major platform for our expansion
                    activities.<br>
                    We will continue to consolidate and constantly enhance our
                    products and services in line with customer expectations.
                    Even as we pursue new avenues, we will ensure that we retain
                    our leadership position in our existing core businesses.&nbsp;<br>
                    <br>
                    <b>OER: </b>Have there been any changes in your business
                    model?<br>
                    <b>Sheikh Saud Bahwan: </b>Cross-shareholding of both the
                    brothers has become 100 per cent individual ownership, and
                    now the two groups are totally independent. The ownership
                    change alone will not result in any distinct change in
                    business models. These will continue to be customer and
                    market-driven.&nbsp;<br>
                    <br>
                    <b>OER: </b>What will be each company's philosophy?<br>
                    <b>Sheikh Saud Bahwan: </b>'Leadership through Excellence'
                    is not only SBG's corporate slogan - but also our working
                    philosophy and has been aptly reflected by our dynamic new
                    logo. We aim for excellence in every field of activity - be
                    it sourcing and bringing the world's best products or
                    providing the highest level of service. We aim to set the
                    highest standards in whatever we do.&nbsp;<br>
                    <br>
                    <b>OER:</b> What will happen to existing employees?<br>
                    <b>Sheikh Saud Bahwan: </b>This division is about
                    shareholding patterns. Hence, unlike the scenario that
                    exists in a takeover or a merger, there is no impact on
                    employees on account of this. In fact, with the new growth
                    that is on the horizon, more operational areas will open up.
                    Hence, over a period, employment opportunities will actually
                    grow. This will also have beneficial consequences,
                    especially, for young, talented Omani youth, which is one of
                    our priority areas.&nbsp;<br>
                    <br>
                    <b>OER:</b> What will be the implication of the your
                    decision to disband in the Omani maket and in the region?<br>
                    <b>Sheikh Saud Bahwan:</b> I think that the division should
                    be seen more as an important milestone in our corporate
                    history. As for the impact on the market, I think it is our
                    continuing commitment to excellence in whatever we do,
                    contributing the best to the nation's progress in all
                    manners possible and keeping Oman's flag flying high - that
                    are important.&nbsp;</font></p>
                    <p><b><font face="Verdana" size="2">Successsful job creation
                    and training records for Omanis</font></b></p>
                    <ul>
                      <li><font face="Verdana" size="2">First company in the
                        private sector to set up a special personnel department
                        to ensure steady Omanisation in all its business
                        activities. The Saud Bahwan Group today employs more
                        than 1,000 Omani staff.</font>
                      <li><font face="Verdana" size="2">The Training Centre set
                        up in 1982 has provided comprehensive training to Omani
                        staff in various functional areas.&nbsp;</font>
                      <li><font face="Verdana" size="2">The Company's Training
                        Centre is the recipient of the &quot;NVQ Centre of
                        Excellence&quot; award conferred by the Adult Learning
                        Inspectorate, UK &amp; the Ministry of Manpower.</font>
                      <li><font face="Verdana" size="2">It has also been
                        honoured with the Institute of the Motor Industry, UK.
                        &quot;International Award for Excellence&quot; for
                        outstanding achievement in Education &amp; Training for
                        the Motor Industry.</font>
                      <li><font face="Verdana" size="2">The Group is the winner
                        of the prestigious Green Card for its outstanding
                        Omanisation record for the past few years. Also it has
                        won the 'GCC Award for Excellence in Omanisation'.</font>
                      <li><font face="Verdana" size="2">The company has various
                        incentive schemes to improve the performance and
                        productivity of its Omani staff.&nbsp;</font></li>
                    </ul>
