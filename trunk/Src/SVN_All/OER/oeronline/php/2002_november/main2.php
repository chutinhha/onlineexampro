                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Ramadhan offers</font><p><font face="Verdana" size="1"><b>Hoping to cash in on the forthcoming Ramadhan season, Oman's businesses are giving final shape to their sales ammunition, and earmarking huge advertising budget. Janaki Raghavan reports</b></font></p>
                    <p align="center"><img border="0" src="../../images/2002_november/ramadan1.jpg" width="400" height="287"></p>
                    <p><font face="Verdana" size="2">Every Ramadhan, Oman witnesses a slew of sales promotions such as raffle draws and gift schemes, so much so that most of the businesses conclude about 50 per cent of their annual sales in this period alone. Though business during last Ramadhan was dull due to a global slowdown, the excitement and action is building up for the forthcoming festive season."<br>
                    We hope to generate good business this season," says Akbar Ahmed Hussein, General Manager, Sabco Commercial Centre, a leading shopping mall in Oman. Hoping to cash in on the festival shopping, retailers are earmarking a huge budget for advertising and promotions. According to Hussein, Sabco intends to spend at least RO25,000 on promotions which is 50 per cent of its annual advertising budget.<br>
                    Muscat Electronics Company (MEC), the distributor for Sony, Supra and Rowa, and Towell Auto Centre (TAC), which sells Mazda cars in Oman, also have huge promotion plans up their sleeve. Though MEC and TAC do not reveal their exact ad spend, industry sources say that the amount involved would be upwards of RO20,000.<br>
                    In fact, for the period from October to December 2001, the ad spend of the automotive sector in the print media accounted for 40 per cent of the total budget for the entire year, while the electronics and retailing industry spent 37.5 per cent and 29 per cent, respectively, says Suhail Mustafa Khan, Manager, Marketing Research, SIMPA, a marketing research and consultancy outfit based in Muscat."<br>
                    Since Eid is the season of big discounts and promotions, people normally go on a spending spree during this time. So it makes sense for the automobile distributors and the consumer durable industry to spend a sizeable amount on their advertising campaigns during this period," observes Ayoob Chankalan, Manager, Oman Advertising, a leading advertising agency.<br>
                    <br>
                    <b>Raffle push</b><br>
                    Sabco, which houses outlets of prestigious international brands such as Nine West and Cerruti, is planning a Grand Festive Promotion from October 1 to January 31, 2003 (also to cover the Muscat Festival scheduled for January next year). The customers can look forward to winning top end cars and gift hampers in raffle draws, informs Hussein.<br>
                    Lulu Shopping Centre is also coming up with the 'Lulu-Musso Mania Offer' to coincide with Ramadhan. Three Musso 4x4s are being offered as prizes in three raffles to be held from October to December. Such schemes indeed push up sales. "About 35 per cent of our annual sales are achieved during Ramadhan," mentions D.R. Bijlani, General Manager, MEC.&nbsp;<br>
                    Apart from retailers, auto companies are also planning several Ramadhan schemes. "Eid is definitely an important season for the auto sector as the market is traditionally buoyant during this period," avers Ashok Bhagnari, Head of Marketing, TAC, which does an average of 25 per cent of total sales during this period.<br>
                    Saud Bahwan Automotive LLC, the franchisee for Toyota, Daewoo and Ford cars, also reveals that Ramadhan is indeed the peak selling period of the year with Ford's sales almost quadrupling during this time. Saud Bahwan Trading LLC, distributors of Daihatsu vehicles, also plans to launch a advertising blitzkrieg to support their grand promotion scheme.<br>
                    Oman Marketing and Services Co. (OMASCO), the distributor of leading electronic brands like National Panasonic, also intends to make the best of the season. They would have sale at all their showrooms from end-October with a 25-50 per cent discount on their entire range of products. Deposits will not be required for the instalment schemes and there will be no interest charged subject to the approval of the financiers, Muscat Finance Co. or Al Omaniya Financial Services.&nbsp;<br>
                    P. Venkat, General Manager, Electronics &amp; Watches, OMASCO, says, "We achieve 35 per cent of our annual sales turnover during Ramadhan. The 35 per cent of our annual budget is earmarked for this period," adds Venkat.<br>
                    <br>
                    <b>Luxury sales</b><br>
                    Lifestyle products and perfumes are very popular gift items during the festive season. Khimji's Mega Store, an upmarket store which sells a range of luxury products including Swarovski crystals, Noritake tableware, and perfumes from Nina Ricci and Chanel will have sale at all their outlets with a 20-60 per cent discount on their products.&nbsp;<br>
                    Jawahir Oman, another upmarket store marketing brands such as Tiffany and other high-end watches and gift items, informs that Ramadhan is the peak season for them. "We will be spending 60 per cent of our annual budget on advertising during these three months," reveals Stephane Malfroy, Marketing Manager, Jawahir Oman.<br>
                    The Muscat Pharmacy, a leading luxury retailer, is offering a 'Gift With Purchase' scheme on the perfumes during Ramadhan. "The demand for cosmetics and perfumes is brisk during the latter half of the month of Ramadhan," mentions Sumithra Raju of the perfumes division.<br>
                    The furniture industry is another segment where sales hit the ceiling during the Ramadhan season. Eid is the time the customer makes a choice of capital assets - be it home-related or a personal belonging such as car or mobile. Most lenders have deferred instalment scheme, interest free loans and even repayment waiver for a month.<br>
                    "The sales double during this period, due to the combined effect of festival sales and price reductions and offers," observes Shankar Naganathan, Divisional Manager, Khimji Ramdas (Furniture Division &amp; General Products Division).&nbsp;<br>
                    <br>
                    <b>Starving hotels</b><br>
                    But not all industries are upbeat about the Ramadhan season. Industry sources say that the restaurant businesses (other than the coffee shops) and the hospitality industry take a beating during the Ramadhan season. "The ban on same beverages and entertainment steals the charm from going to a high-end restaurant," mentions an analyst."<br>
                    However, that doesn't stop them from offering freebies. For instance, PavoReal, an upmarket Mexican restaurant, will be offering a discount voucher during the month of Ramadhan which will entitle the customer to get a 10 per cent off during his subsequent visit to the restaurant.<br>
                    Radisson SAS Hotel, a top-end hotel in Muscat, has another strategy. "Our main Ramadhan activities are centred around Iftar buffet evenings, where we tailor the cuisine to suit the special time of year. Eid is very much a time to target inter-regional leisure travellers from markets such as Dubai, Kuwait, Bahrain and Saudi Arabia," claims Marc Fletcher, Director of Sales and Marketing, Radisson.<br>
                    With expectations running high for the forthcoming Ramadhan, consumers can look forward to great deals on every purchase while businesses hope to walk laughing to the bank.</font></p>
