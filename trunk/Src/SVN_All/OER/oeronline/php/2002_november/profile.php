                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Profile
                    - Driving ambition</font><p><font face="Verdana" size="1"><b>Adil Said Al-Shanfari is Vice Chairman of the Shanfari Group, one of Oman's largest conglomerates. He talks exclusively to OER about how he plans to ensure the family business retains its place among the country's leading companies. Nayl D'Souza reports&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_november/Shanfari-1.jpg" align="left" hspace="5" vspace="5" width="156" height="200"></font><font face="Verdana" size="2">Its another sunny day in Muscat but one gets the impression that even if the skies had opened up to unleash a torrent of hailstones, the charming Adil Al-Shanfari would not be phased one jot. He is self-confident yet down-to-earth. His staff say that he values trust, openness and consensus. Today, he is even a little jovial although he denies it in a moment with a cheeky smile and instantly switches to a more reflective posture to happily discuss, at length, how he managed to secure national franchises to market some of the best sports car brands in the world despite fierce competition and how he intends to continue restructuring the company to help it remain competitive.<br>
                    Established over 30 years ago, the Shanfari Group has grown from a small trading company to become one of the largest business houses in Oman with over 25 companies representing a diverse range of activities from construction to manufacturing, and from tourism to transport. The company employs around 5,000 people. Shanfari Automotive Co., one of the conglomerate's flagship companies, exclusively markets prestigious brands such as Aston Martin, Ferrari, Maserati, and Lamborghini. Away from the luxury sports segment, the automotive division also represents the middle range of the saloon market with Alfa Romeo, Fiat and Saab although it is the merchandising of the Jeep brand that insiders say provides the company's main 'bread and butter' although exact sales figures are unavailable.<br>
                    Adil Shanfari first joined the Shanfari Group in 1990 and helped establish the trading name Shanfari Automotive Co. (it was formerly known as Shanfari Trading Co.). At around the same time, Chrysler acquired Jeep. The company already had the franchises for Italian car brands Ferrari and Lamborghini but Adil was determined to seize new opportunities. He flew to Detroit in order to convince the US auto manufacturer that Shanfari Automotive would be the best candidate to become the sole representative for Jeep in Oman despite the fact that the Zubair Group successfully showcases the Chrysler brand. He succeeded in obtaining the exclusive agency rights for the Jeep brand in the Sultanate.&nbsp;<br>
                    "Chrysler had the chance to choose any company to represent Jeep. It was a difficult task to convince them that we were the right people but we did and we've never looked back since," Adil tells OER. "The key to our successful presentation was to show them sales figures that were achievable rather than portray something that was impossible."<br>
                    The introduction of Aston Martin (recently acquired by US car giant General Motors) to the fold a few months ago has helped boost the Group's automotive exotica which also represents Harley-Davidson Motorcycles in a purpose built outlet located in Al-Khuwair. The showroom displays a full range of motorcycles, accessories and clothing.<br>
                    Aside from representing sports cars, four wheel drives, saloon cars and motorcycles, the automotive division used to offer motoring solutions until earlier this year, the company decided to wind up Payless Car Rental locally. Adil Shanfari explains that such decisions to restructure may not be popular but are necessary to keep the Group competitive.&nbsp;<br>
                    The formation of a new executive committee to work alongside the board of directors is another of the company's recent sweeping changes. Adil says his father Sheikh Said Ahmed Al Shanfari, Chairman of the Shanfari Group, has given the new think tank, which manages day-to-day operations, his blessing. He says his advisors have succeeded to demonstrate much understanding of the company's values and history of innovation.&nbsp;<br>
                    "I really understand where my father wants to take the business," he says. "In the past year, we've already embarked on a large restructuring of the whole Group and implemented changes that will make it more modern. This has left more time for my father to take control of broader business strategies as well as carry out social engagements."<br>
                    The committee, chaired by Adil, consists of the company's most key professionals and trusted advisors. Its foundation has ensured that more personnel than just family members share the key decision-making processes.&nbsp;<br>
                    "I'm not an autocratic manager. It's a team effort. We have brought in expertise and given them more responsibilities in some respects but the buck stops with the new executive committee. Since its introduction, the company has run a lot smoother and has continued to innovate."<br>
                    "My door is always open if anyone has a problem. I don't distinguish myself from other managers in the company, and most I regard as friends," he adds noting that the Group has also improved its internal auditing process in the head office and the staffing of this department in order to tighten up on accounting procedures and better support divisions to keep within their allocated budgets.<br>
                    On future efficiency drives, he vows to continue the company's reforms to improve the Group's success in responding quickly to market conditions and client needs. "We need to consolidate on the gains we have made in the market and ensure each division operates to its maximum efficiency to help them grow."</font></p>
