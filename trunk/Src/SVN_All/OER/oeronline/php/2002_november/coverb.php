                    <font face="Verdana" size="4" color="#FF0000">GOING SOLO</font>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_november/suhail_logo.jpg" align="left" hspace="5" vspace="5" width="100" height="72">&quot;The Group's new
                    logo encapsulates 'Suhail' in Arabic - the most luminous
                    star which has given direction to many leading them to their
                    different destinations. It represents the guiding star that
                    leads the Group today. It reflects the waves of changes that
                    have to be crested and the dunes of challenges that have to
                    be scaled to keep shining in a dynamic and turbulent
                    business universe. It embodies the harmony and unity of the
                    circle. The abi lity to hold and inspire trust in our
                    business partners.&quot;&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_november/cover-Suhai.jpg" align="right" hspace="5" vspace="5" width="152" height="174"></font><font size="2"><b><font face="Verdana">The Suhail Bahwan
                    Group</font></b><font face="Verdana"><br>
                    Today, the Suhail Bahwan Group is a well-diversified
                    business conglomerate with 25 companies under its wing. Its
                    activities include consumer products, manufacturing, power
                    generation, fertilizer, electronics, pharmacy, shipping,
                    building material, furnishing, travels, food products,
                    contracting and information technology, among others. The
                    flagship company Bahwan Engineering Company employs over
                    3,500 personnel including more than 125 engineers. Other
                    companies include Bahwan Building Materials LLC, Bahwan
                    Contracting Co. LLC, Bahwan Foods (Khalijana) Co. LLC,
                    Bahwan Trading Co. LLC, Bahwan Travel Agencies LLC,
                    Electronic Co. LLC, Bilad Oman Co. Ebin Rushed Pharmacy Co.
                    LLC, Gulf Business Machines Co. (Oman) LLC, International
                    Equipment &amp; Contracting Co. LLC, Rent A Car LLC, Sur
                    Travel Agencies LLC, Sogex Oman Co. LLC, Sohar International
                    Urea &amp; Chemical Industries SAOC and Universal
                    Engineering Services LLC.&nbsp;</font></font></p>
                    <hr SIZE="1">
                    <p><font size="2"><b><u><font color="#ff0000" face="Verdana">Interview</font></u><font face="Verdana"><br>
                    <a name="Sheikh Suhail Bahwan">Sheikh Suhail Bahwan</a></font></b><font face="Verdana"><br>
                    Sheikh Suhail Salim Bahwan, the Chairman of the Suhail
                    Bahwan Group, is an entrepreneur par excellence. OER speaks
                    to him about how he sees the division and what future
                    entails for him.&nbsp;<br>
                    <br>
                    <b>OER:</b> Could you tell us about the 'amicable
                    settlement' you have reached with your brother Sheikh Saud
                    Bahwan?<br>
                    <b>Sheikh Suhail Bahwan:</b> My brother Saud had been
                    looking after the entire range of businesses under the
                    umbrella of Bahwan Auto Centre while I had been managing
                    diverse businesses such as consumer durables, watches and
                    personal items, building materials, travel, civil,
                    mechanical and electrical work, construction,
                    telecommunications, information technology, health care
                    products, food, shipping and transport. Since a major
                    portion of business was done through Bahwan Group, we felt
                    the need for ensuring smooth transition to future
                    generations.&nbsp;<br>
                    <br>
                    <b>OER: </b>By future generations, you mean that the
                    business would pass more easily on to each of your children?<br>
                    <b>Sheikh Suhail Bahwan: </b>Yes. My children have been with
                    me in business for a number of years. They are the biggest
                    assets of our Group and ensure continuity.&nbsp;<br>
                    <br>
                    <b>OER:</b> What are the challenges and opportunities ahead
                    for each entity?<br>
                    <b>Sheikh Suhail Bahwan: </b>I was not waiting for any
                    settlement to look for new opportunities. The existing
                    business has been growing every year - mainly because of our
                    professional approach and the importance that we attach to
                    customer satisfaction. We have been able to cash in on new
                    opportunities like the privatisation of the power sector and
                    airports and by entering into the industrial sector through
                    the proposed fertiliser project in Sohar. Both of us are
                    oriented towards excellence and customer satisfaction. We
                    continue to remain so.&nbsp;<br>
                    <br>
                    <b>OER: </b>What kind of changes will take place in the
                    management structure?<br>
                    <b>Sheikh Suhail Bahwan:</b> Saud Bahwan will continue to
                    manage his Group and I will manage mine.&nbsp;<br>
                    <br>
                    <b>OER:</b> Which direction will your group take?&nbsp;<br>
                    <b>Sheikh Suhail Bahwan:</b> We are a highly diversified
                    group in Oman with a presence in all areas from trading to
                    construction. For the past few years, I have been focussing
                    on projects of national interest like Barka power project
                    and airport expansions. While trading is an important aspect
                    of business, it is industrialisation that will take the
                    nation forward. Hence, our involvement in the proposed
                    fertiliser project in Sohar. These projects will initiate
                    Oman's private sector into a new era of industrialisation.&nbsp;<br>
                    <br>
                    <b>OER: </b>Do you envisage any changes in business models
                    now?&nbsp;<br>
                    <b>Sheikh Suhail Bahwan:</b> Diversification is a continuous
                    process and is not dependant on any settlement. I will
                    continue to manage all the companies, which were under my
                    fold. My focus is on the projects of national interest and
                    the industrialisation of Oman. What is good for Oman is good
                    for the Suhail Bahwan Group.&nbsp;<br>
                    <br>
                    <b>OER:</b> What were the difficulties you faced when it was
                    one entity?<br>
                    <b>Sheikh Suhail Bahwan: </b>There were no management
                    difficulties at any time.&nbsp;<br>
                    <br>
                    <b>OER:</b> What will be the implications of the division of
                    the group for Oman and the region?<br>
                    <b>Sheikh Suhail Bahwan:</b> My brother and I will continue
                    to lead two groups and run them in a professional manner for
                    the benefit of our customers and serve our nation by
                    boosting its economy.&nbsp;<br>
                    <br>
                    <b>Successsful job creation and training records for Omanis</b></font></font></p>
                    <p><font face="Verdana" size="2">Highlights of the Suhail
                    Bahwan Groups' flagship Bahwan Engineering Company (BEC) LLC
                    and growing corporate subsidiary Bahwan CyberTek&nbsp;</font></p>
                    <ul>
                      <li><font face="Verdana" size="2">BEC celebrates 25 years
                        of establishment by announcing that it has contributed
                        RO250,000 towards the Technical Education Fund over the
                        next four years to be used to train and develop
                        deserving Omani nationals in line with the SANAD
                        programme and in close co-operation with Ministry of
                        Manpower.</font>
                      <li><font face="Verdana" size="2">BEC is the first company
                        in Oman to obtain ISO 9002 accreditation for Project
                        Management and Construction Activities.&nbsp;</font>
                      <li><font face="Verdana" size="2">Bahwan CyberTek has
                        inducted 47 Omani students on a one-year intensive IT
                        development programme to eventually sit exams to become
                        Oracle Certified Professionals.&nbsp;</font></li>
                    </ul>
