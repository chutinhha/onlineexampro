<img border="1" src="blue_city.jpg" width="450" height="111"></p>
<p align="justify" dir="ltr"><b><font face="Verdana" size="3">L</font></b><font face="Verdana" size="1">ast 
month, we saw the launch of ‘Al Madina Al Zarqa’ (The Blue City) project aimed 
at putting Oman on the forefront of the world tourism map. Touted as one of the 
largest economic and real estate development projects in the Gulf region, The 
Blue City venture is expected to cost $15 billion and get completed in a 15-year 
timeframe. <br>
<br>
The Blue City is a joint initiative of local, regional and international private 
sector investors and developers and the Government of the Sultanate of Oman. It 
is promoted by Al Sawadi Investment &amp; Tourism (ASIT) LLC. The major shareholders 
in ASIT are AAJ Holdings Company, Bahrain and Cyclone LLC, Oman. Al Madina Al 
Zarqa is also backed by a Royal Decree that designated it as a strategic tourist 
zone.<br>
<br>
Ahmed Abubaker Janahi, Vice-Chairman, ASIT and Chairman and Chief Executive 
Officer of AAJ Holdings Company said, “Our vision is turning into a reality. We 
successfully finalised the entire urban economy master plan, negotiated and 
secured required financing to develop the project from international sources and 
have received tremendous interest from anchor hotel and leisure operators such 
as Marriott, St. Regis of Starwood, Raffles, Campbell Grey Hotels currently 
operating Carlisle Bay Hotel in Antigua and OneAldwych in London and many other 
names.”<br>
<br>
He added, “We have ensured to apply all necessary measures for the project to be 
friendly to the environment with unique lifestyle, reflecting the heritage and 
authenticity of Oman.” ASIT is expected to opt for listing on Muscat Securities 
Market (MSM) in the near future. <br>
<br>
The project will be completely financed by the Swiss-based Oppenheimer of North 
America, one of the largest financial houses in the world, along with other 
consortium partners. As per Prof. Jerald Alan Belofsky, CEO, Oppenheimer, the 
company will issue bonds to raise desired funds for the project. He further 
said, “The vision behind Al Madina Al Zarqa is simply breathtaking. The world 
will witness the rise of a completely new urban setting that will certainly have 
a tremendous impact on the whole region. <br>
<br>
“When we heard about this fabulous opportunity, we hurried to contact the 
principals of ASIT and did our utmost to be awarded the engagement for providing 
finance.” <br>
<br>
The dream project is going to be based on a seafront site covering 35 sq km at 
Barka, located 30 minutes from Oman’s international airport. On the project, 
Anees Issa, Chairman and Managing Director, Al Sawadi Investment &amp; Tourism 
(ASIT) LLC said: “Blue City will be home to more than 200,000 permanent 
residents. It is going to be a tourism and business destination with immense 
economic and social benefits for the country.”<br>
<br>
The construction for the first phase of the project, costing $1.8 billion, is 
expected to start in the last quarter of this year. In the first phase, the 
focus will be on hotels, residential and other tourism related infrastructure 
targeted at the high-end of the market segments. By the end of the first phase, 
The Blue City covering 8 sq km area will have 4.5 km of beach and waterfront, 
and three beach resort hotels, one golf resort hotel, and one city beach 
resort/conference hotel. Besides that, the city will be marked by a 36-hole PGA 
Championship Golf Course with 600 luxury villas. There will be 1,000 waterfront 
residential resort units along with Arabian style tourist village with souqs, 
apartments and boutique hotels. Besides that, The Blue City will have other 
attractions including a large aquarium and amphitheatre besides boulevards, 
waterfront promenades, squares and parks. <br>
<br>
The Blue City is expected to drive tourism in Oman in a big way. Oman currently 
has 6,000 hotel rooms and with the completion of first phase of The Blue City, 
2,000 more hotels rooms will be added. Mohsin bin Khamis Al Baloshi, 
Under-secretary, Oman Tourism Ministry, informed, “We received 1.1 million 
tourists in Oman in 2003. And we are hopeful that once The Blue City project is 
over we are going to get lot more tourists in the country.” <br>
<br>
He further added, “The tourism sector had generated $284 million in 2003. The 
Blue City will give a new dimension to the real estate development and tourism 
sectors. This project really fits in Oman’s long-term vision for 2020 under 
which the tourism sector will be totally decentralised by attracting more FDIs, 
creating more job opportunities and increasing the contribution of this sector 
to 10 per cent of the GDP from one per cent. <br>
<br>
“The new project will also add value to the culture and heritage and the 
preserve environment – one of the most daunting challenges faced by other 
countries.”<br>
<br>
The development of The Blue City is going to give further momentum to the 
burgeoning real estate market. Additional 1,000 homes will come up. The 
foreigners will be allowed to purchase property in The Blue City and they will 
also get freehold ownership besides renewable residence visas for the buyer and 
close relations like wife/husband, children and parents. It will also generate 
new avenues for employment as atleast 7,000 direct and 25,000 indirect jobs will 
be created in the first phase itself. <br>
<br>
It will be interesting to see how The Blue City’s vision turns into a reality as 
most of the Gulf countries have initiated urban development mega projects to 
enhance their tourism, real estate and business potential. Dubai is developing 
the 81 million sq mtr beachfront ‘Dubai Waterfront’, the world’s biggest 
waterfront development perhaps even larger than Manhattan. Back home, The Wave, 
a mega tourism and seafront residential development project near Seeb 
International Airport, is also launched which is expected to get complete before 
The Blue City project.<br>
