            <p align="justify" dir="ltr"><font face="Verdana" color="#FF0000">
            <span style="font-size: 16pt; font-weight: 700">Luxury on wheels </span></font><font face="Verdana" size="1"><b><br>
            <br>
            Whether it’s the design, luxurious feel or the precision technology, 
            the new Cadillac STS scores high in all categories says Anne Kurian</b></font></p>
            <p align="center" dir="ltr"><font face="Verdana" size="1"><b>
            <img border="0" src="cadila.jpg" width="300" height="131" hspace="5" vspace="1"></b></font></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
            brand new Cadillac STS is the king on the Oman roads these days. 
            Luxurious, comfortable and regal, the Cadillac STS is an attention 
            grabber all the way. <br>
            <br>
            Be it the design, the luxury factor, the driving experience or the 
            precision technology, Cadillac STS scores high points in every 
            category. Cadillac STS is loaded with features that leave you mighty 
            impressed with the luxury sedan. <br>
            <br>
            The STS comes in two variants — the 3.6L 255hp V6 VVT and 4.6L 320 
            hp Northstar V8 VVT. We test-drove the Cadillac STS V8 and to sum up 
            the experience in a few words, it was exhilarating.<br>
            <br>
            The engines have been designed to deliver smooth and definite flow 
            of horsepower and torque, which is not the so in traditional engine 
            design. The result? It takes absolutely no time for you to move from 
            0 to 100. The speed and pick-up is remarkable.<br>
            <br>
            The STS V8 boasts of All Wheel Drive that elevates the driving 
            experience and brings it on par with some of the other luxury cars 
            in the market. <br>
            <br>
            <b>Exteriors</b><br>
            Though my friend and I would have liked to think that all admiring 
            glances were directed at us, all attention on the road was on the 
            STS. Fitted with a longer wheelbase and also body than the CTS, the 
            new STS looms large on the roads. The signature front grille and 
            pointed front, common in the Cadillac family, stays put in this 
            model too. <br>
            <br>
            The design of the sedan is slightly boxy but it has an aesthetic 
            form that gives the STS quite a classy feel. Its wide stance and 
            long wheelbase with short front and rear overhangs give the STS 
            visual strength.<br>
            <br>
            Moving onto other features, the sedan is powered with High-intensity 
            discharge headlamps (HID), headlamp washers and LED tail lamps. 
            Powerful high beam lights to guide you through the dark and dimly 
            lit roads in the interiors of Oman. One of the interesting features 
            of the STS is the Intellibeam system — the headlamps switch on and 
            off automatically depending on the natural light around. <br>
            <br>
            The STS is fitted with ultrasonic rear parking assist that has an 
            LED indicator and also audible warning. Automatic wipers are yet 
            another feature of the STS, which is built-in with rain sensors. <br>
            <br>
            Much like many other American models of cars, the STS also is 
            equipped with dual exhaust pipes at both ends of the rear bumper. 
            The deck-lid spoiler completes the sporty look of the sedan. The 
            model also comes with sunroof that boasts of one-touch open and 
            close facility. <br>
            <br>
            <b>Interiors</b><br>
            The interiors of the STS are an embodiment of comfort and luxury. 
            Tuscany leather for the seating, wooden trimmings and roomy 
            interiors; what more could you ask for? Knowing its Cadillac, 
            there’s a lot more than they deliver without asking. One such 
            feature that deserves mention is the front bucket seats that have 
            eight-way power adjustment facility for maximum comfort. The 
            driver’s seat position can also be stored in the car’s memory, which 
            is another unique feature of the STS. <br>
            <br>
            The STS is roomy and has ample seating and leg space. Though the car 
            ideally accommodates 2 + 3 passengers, there was not a squeak of 
            complaint from any of the four passengers who came along for a 
            joyride in the STS. The STS can quite comfortably seat four 
            passengers in the backseat. That’s spaciousness for you.<br>
            <br>
            Impeccable interior layout makes the STS a pleasure to drive and 
            operate. The various controls on the dashboard are easy to locate 
            and are positioned in a pleasing-to-the-eye style. As with most 
            luxury cars, the controls for the radio/music and cruise are also 
            positioned on the steering wheel. <br>
            <br>
            The STS has to its credit of being the first luxury sedan fitted 
            with the 5.1 Bose Studio Surround system. The 5.1 surround system 
            uses six separate channels — left, right, centre, left-rear 
            surround, right-rear surround and low frequency effects. In short, 
            what you get is unmatched audio experience. And to top it, the car 
            has no less than 15 speakers placed on the dashboard, doors, front 
            seats and behind the backseat. <br>
            <br>
            <b>Technology and safety</b><br>
            Cutting edge technology forms the core of the STS. To begin with, 
            the STS can be transformed into a Bluetooth-competent phone. Connect 
            your Bluetooth-enabled phone to the electronic system of the STS and 
            you can make calls through the monitor. You can even save your 
            favourite numbers on the system for convenience. How’s that for 
            advanced technology? <br>
            <br>
            But it does not stop there. The STS comes en suite with advanced DVD 
            navigation system and GPS (Global Positioning Satellite) that 
            provides you with exceptional facilities. The colour VGA display 
            screen, with four-position tilt, can be viewed in four different 
            background shades. <br>
            <br>
            Another first for the STS is the six-disc DVD changer to be offered 
            in an automobile. Of course, the DVDs can be played only when the 
            vehicle is not in motion. <br>
            <br>
            What takes the cake in this luxury sedan is the use of GPS 
            technology. The STS is engineered to not just display the location 
            of your car but also the route to your destination. And if you 
            happen to be searching for, say…a fast food joint in the area you 
            are and don’t know where to find it, all you have to do is type it 
            into the system of the car. The STS will show you the route to the 
            nearest fast food restaurant in the area you are in. After all, your 
            wish is the STS’s command. This facility is applicable not just in 
            Oman but also in UAE.<br>
            <br>
            Another outstanding feature of the STS is the Adaptive Remote Start. 
            The key becomes an object of the past with the STS. The key is not 
            required to operate the STS. The STS remote keyless fob lets you to 
            start the engine and activate the presets even when you are more 
            than half a football field away. By the time you reach the car, the 
            A/C would be switched and your favourite CD will be playing. Just 
            what you’d require during the scorching summers of Oman. You also do 
            not require the key to start the engine when you are inside the car; 
            the fob allows you to bring the engine to life by just pushing the 
            control on the dashboard. There’s no doubt that Cadillac means 
            serious business with the STS.<br>
            <br>
            These guys also have taken immense care about the safety factor. The 
            Panic Brake Assist ensures that when you brake suddenly and hard all 
            the four wheels are brought to a stop at the shortest distance and 
            time. As for cruise control, the Adaptive Cruise Control (ACC) 
            allows you to preset the speed you wish to travel at. What’s 
            commendable is the radar sensor behind the crest of the STS that 
            adapts to the preset speed and automatically adjusts depending on 
            the traffic. Just in case someone cuts you unexpectedly, ACC brakes 
            and warns you audibly as well as visually to apply the brakes 
            harder. Six airbags and tire pressure sensor that warns you anytime 
            there’s low pressure in the wheels adds to the safety features of 
            the STS. <br>
            <b><br>
            Last impressions</b><br>
            The STS is not just a great looking sedan but also one that 
            delivers. Even at high speed the car moves with smoothness that is 
            only imaginable. The features of STS makes it an automotive that can 
            double as a family sedan and also a fun, sporty car to zip off on 
            the Oman roads.
