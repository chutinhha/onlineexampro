<p align="justify"><font face="Verdana" size="1"><b>Solid Content</b><br>
The key to the success of your newsletter lies primarily in its content. Make no 
mistake – you aren’t the only organisation with a newsletter, these days there’s 
a great deal of competition out there. Recipients may well be happy to subscribe 
to more than one list but will only continue their subscription if you offer 
them something of value. <br>
<br>
So what makes good newsletter content? In my view, make it chatty and informal. 
But chatty doesn't mean airy. You need to say something worth saying. Remember, 
your job is to add value to your readers' lives, not frustrate or bore them. One 
thing newsletters aren’t about is selling. Build a newsletter around a sales 
pitch and it’ll die a very quick death. <br>
<br>
What type of content should you offer? A lot of this depends on your industry. 
What do your readers want/need to know? This certainly isn’t necessarily what 
your sales team wants to tell them. Put yourself in your readers’ shoes and 
think about what valuable information you could offer them. At least one 
in-depth feature article per issue is important, this can be supplemented by 
other content sources such as an interview with an industry expert, a case study 
or FAQ. How-to write articles are always popular. <br>
<br>
If you think about it, you're probably the local world's authority on something, 
at least your readers will think so if you explain the fine points to them in an 
article. If you run industrial estates, you'll talk about lean manufacturing, 
the impact of low wage economies on Oman’s manufacturing future, your tenants 
activities and products. You’ll look at the major trends affecting manufacturing 
and you’ll suggest training programs, websites, conferences and workshops that 
manufacturers will be interested in. I recommend that 80% of your content should 
be hard-hitting information delivered in a readable editorial style. Be 
inventive and interesting.<br>
<br>
Don't think you have to write all the articles yourself. If you don’t have the 
resources in-house to develop such content then hire an expert - paying for a 
good copywriter can pay excellent dividends. <br>
<br>
If you do have writing talent in-house then use it. Developing a personality for 
the newsletter is also a good way to make it stand out from the crowd. Think of 
your newsletter as a magazine or newspaper - give it distinct sections so people 
can build up a relationship with it and skim read it easily.<br>
<br>
<b>Launching Your Newsletter</b><br>
The first step to launching your newsletter, as with any marketing campaign, is 
to understand who your audience is. The best way to get results is to decide 
what objective you’re looking to fulfil and target your subscriber base 
accordingly. In many cases, the best place to start is with your current 
customer data base – they’re the folk that have shown an interest in your 
product or service and you’ve already established a level of knowledge and trust 
with them. There are a number of ways in which you might choose to build 
subscriptions to your newsletter - techniques include: </font></p>
