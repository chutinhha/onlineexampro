<p align="justify"><font face="Verdana" size="1"><b>Communicate</b><br>
One of the most successful marketing techniques available today is a solid, 
informative, well-written newsletter – either hard copy or via e-mail. Indeed, 
over the years many organizations have turned around the success rate of their 
marketing campaigns with the launch of a regular and hard-hitting newsletter.
<br>
<br>
Every organisation needs a boost – whether it’s more leads, sales, increased 
profits or bigger customers, a well planned and executed newsletter can help you 
achieve the desired results. Today, successful businesses of every kind are 
built on solid customer relations and newsletters are the ultimate in 
relationship marketing. They enable you to connect with customers, prospects and 
stakeholders in a form that offers them the opportunity to engage with you. A 
newsletter establishes credibility as well as engendering a feeling of trust 
that, overtime, can be turned into loyalty and good custom. Why? Because the 
recipient of your newsletter actually wants to hear from you and if properly 
conceived and implemented will actually look forward to seeing what you have to 
say. Let’s face it, a newsletter is far less threatening than a telemarketing 
call and more likely to be read than a traditional direct mail shot. <br>
<br>
Now this all sounds very promising but newsletters aren’t an easy option and it 
can take a lot of time and effort to build a subscriber base – but for smart 
marketers the thought of capturing and connecting with a genuinely interested 
audience is irresistible. <br>
<br>
<b>What’s a Newsletter?</b><br>
I’ve seen a number of newly-launched Oman-based corporate newsletters – hard 
copy and e-mail versions – PEIE Mirror and PEIE Brief (both put out by the 
Public Establishment for Industrial Estates) are notable examples. But what’s 
the purpose of a corporate newsletter and how do you go about piecing one 
together? Newsletters come in all shapes and sizes and help to achieve many 
things. They keep staff, clients and stakeholders informed of projects and 
activities; they motivate staff; promote projects and services; celebrate 
success; share good practice; and offer 'added value' to clients and customers. 
In simple terms, a newsletter is a documentation and communication product that 
provides a regular update about an organisation or project. It can:</font></p>
