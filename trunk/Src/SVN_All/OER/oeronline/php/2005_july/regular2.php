<p align="justify"><font face="Verdana" size="1">Some of these methods will work better for you than others and most 
  organisations use a mix of different techniques until they hit upon the most 
  successful. It’s often well worth gathering subscriber details ahead of your 
  launch. For instance, a sign-up box on your website can be added six weeks in 
  advance, simply make it clear to recipients when they can expect to receive 
  the first edition.</font></p>
<p align="justify"><font face="Verdana" size="1"><b>Sample Newsletter</b><br>
Here are some tips on what a newsletter should look like:<br>
<br>
<b>Front Cover</b><br>
Name of the newsletter, date, edition number, logo. Introduction to your 
organisation. Lead article - outlining your experiences and position on the 
chosen theme; clear heading; photo; and fact box. In general terms, choose a 
theme for your lead article that’s topical, interesting for the readers and 
relevant to your organisation’s work. Don’t write articles that are too long. 
Focus on two or three key messages and use short paragraphs, photos and boxes to 
make the text attractive and easy to read. <br>
<br>
<b>Page 1 </b><br>
Four to six brief news updates about your organisation and its activities. Keep 
news and updates brief, topical and relevant to readers. <br>
<br>
<b>Page 2 </b><br>
Case study of a project/event, a member of your staff or a business partner 
showing what the chosen theme (e.g. lean manufacturing) means in practice – 
include the all-important photos. <br>
<br>
<b>Pages 4 - 6 </b><br>
Interview with a local stakeholder - sharing their views about lean 
manufacturing. Make sure the newsletter includes a wide variety of stakeholders, 
folk linked to your organisation. Indeed, let them contribute ideas and 
information to the newsletter. <br>
Keep your language simple and encourage those being interviewed to do the same. 
Definitely avoid abbreviations and jargon. <br>
<br>
<b>Page 7 </b><br>
Letters from readers, lists of resources such as training programmes, websites, 
books and magazines that you think would be of interest and value to your 
readers. It’s important that you keep a balance and not just run positive 
letters and feedback. <br>
<br>
<b>Page 8 </b><br>
Calendar of events and your contact details. Let people know how they can give 
feedback about the newsletter or get more copies.<br>
<br>
<b>Style </b><br>
What about the newsletter’s style and design – how you can make your newsletter 
as interesting and attractive as possible?<br>
<br>
Newsletters should be written in simple English using short sentences and 
written as you would speak. Compose articles with the most important information 
first and the least important last. The first few paragraphs should answer: <br>
• Who? <br>
• What? <br>
• Why? <br>
• Where? <br>
• When? <br>
• And how? <br>
<br>
Make sure the newsletter’s style complements your other corporate materials, 
such as brochures, website, pop-up banners, billboard ads, leaflets and annual 
reports.<br>
<br>
Your audience may be staff, clients, potential clients or stakeholders. If you 
produce an all-purpose newsletter it’s important to consider the needs and 
interests of all members of your audience. <br>
<br>
Think of a name for your newsletter that has a connection to your organisation. 
Make sure it’s easy to remember and isn’t being used by another group. It’s 
important that the newsletter is easy to use and has clear structure for its 
readers to follow. For example, number the pages and include a contents list.
<br>
<br>
Choose one person to edit the newsletter – this’ll guarantee that style is 
consistent throughout. <br>
<br>
Use visuals to liven up your newsletter. Make sure each one illustrates a 
specific point and has a caption explaining what it is. <br>
How often you choose to publish your newsletter will depend on its content and 
the audience, generally speaking once a month is about as frequent as most 
organisations can manage. Never over promise and under deliver. Remember, this 
is all about creating trust and fostering better customer relationships. If your 
content is good then not only will customers want to read what you have to say, 
they’ll look forward to receiving your newsletter. <br>
<br>
<b>Wrap Up</b><br>
If you’re serious about launching a newsletter then these are the key points you 
should be thinking about.</font></p>
