<p align="justify"><font face="Verdana" size="1">
<b>
Faisal Al Hashar likes challenges. This love for challenges has made him take on 
different responsibilities with gusto all through his period of work. Anne 
Kurian profiles his persona</b><br>
<br>
</font><font face="Verdana" size="3">
<b>
<img border="0" src="personal1.jpg" align="left" vspace="1" hspace="5">T</b></font><font face="Verdana" size="1">he 
External Affairs Manager of Shell Representative Office Oman, Faisal has donned 
different hats during his career and each experience has left him enriched. 
Although Faisal was born in Sur, he did his early education in Dubai where his 
dad was working. Faisal's family moved back to Muscat during the Renaissance and 
he continued his education at Saidiya School. In 1976, he left for United 
States, where he completed his last high school year in California. His interest 
in taking on challenges was quite evident from Faisal's college days. <br>
<br>
&quot;I changed my subject of major three times because I thought them to be ordinary 
and not interesting enough. After high school, I joined for industrial 
engineering at Cuesta College in California. But I realised it was too common a 
subject and went in for management studies. This also did not appeal to me and I 
then attended a class in marketing. A teacher who was extremely passionate about 
her subject took this class and left me feeling very impressed. She convinced me 
to take up marketing as my subject of study. I majored in marketing at the 
Northwest State University, Missouri,&quot; says Faisal.<br>
<br>
He came back to Muscat after receiving his Bachelor of Science in Marketing and 
joined the Public Authority of Marketing Agricultural Produce (PAMAP) in 1982. 
&quot;At that time, PAMAP was just being set up and there were just 10 of us in the 
company. I was one of the only two Omanis who were working in PAMAP. I was 
jointly responsible for setting up of the infrastructure, price regulations and 
drawing up the marketing strategy. Then PAMAP went through a stage when the 
infrastructure had to be worked upon and built. I left at that time and tried 
the insurance business through an agency with one of the local major insurance 
companies,” he says. <br>
<br>
Faisal joined PAMAP again in 1986, this time as the Director of Sales and 
Marketing, and with many challenges to face ahead. &quot;For me, it was the time when 
things started to get better, though as a company we had to face a lot of 
challenges. For example, we had to deal with large quantities of locally 
produced goods that had to be distributed all over the country according to 
demand in a very short period of time. This required careful planning and prompt 
execution,&quot; he says. <br>
<br>
In 1989 Faisal was appointed Director General of PAMAP. &quot;By then the company had 
grown a great deal, we had 450 employees and 20 collection-distribution centres 
all over Oman. It was one of the most interesting periods of my career, as I got 
to visit all these centres and in turn got to see Oman also. We had a 
magnificent team, who was dedicated and did their job well. Late hours followed 
by early hours were sometimes the norm of our work, but everyone did his or her 
best. To top it, the Omanisation level was over 80 per cent.&quot;<br>
<br>
After working in the government sector for many years, Faisal shifted to the 
private sector in 1997. He joined the Tawoos Group as General Manager, 
Marketing. &quot;It was a different experience for me. There were different 
challenges and the field I had opted for was different. I was given the 
responsibility of projects development and later handle the media and 
distribution services group of Renaissance Holdings SAOC (previously called) 
which included United Media Services, Memo Express in Dubai, United Publishiing 
and Centennial Publishing in the UK. <br>
<br>
&quot;One of the projects was setting up of United Press and Publishing, under which 
magazines like Oman Economic Review and Adventure Oman came. We also came up 
with the idea of bringing out Al Mar'a as every publishing house's success lies 
in the range of magazines it brings out, which gives better economies of 
scales,&quot; he says.<br>
<br>
At Renaissance, Faisal took on responsibilities in different fields like 
publishing, advertising, media marketing and distribution. So when the offer to 
join Shell in November 2000 came along as commercial manager, it had him 
pondering over the offer. &quot;Shell was different from all the other sectors I had 
worked in. But again this was something I would have liked to try my hand at as 
it was a new experience,&quot; he says.<br>
<br>
However, Faisal had to turn down the offer. &quot;I was in the middle of preparing my 
group’s business plan for 2001 and closing that year’s business plan. But fate 
seemed to play a different game. I got another offer from Shell in February 2001 
and this time as Corporate Affairs and Business Development Manager. I thought 
that opportunity does not knock twice and if I had to get an offer from Shell a 
second time, then there must be something in it. So I resigned from Renaissance, 
and joined Shell. <br>
<br>
<b>
<img border="0" src="personal2.jpg" width="100" height="102" align="left" hspace="5" vspace="1">A new world</b><br>
&quot;Shell was a totally different world. The work and the fields of management were 
different from what I had done previously'&quot; he says. Faisal worked together with 
the then Managing Director, Nick Patterson. Together they formulated a growth 
strategy for Shell Oman Marketing and also developed plans for handling investor 
relations. <br>
<br>
&quot;My greatest focus at that time was to find a way to align ourselves at Shell 
with the issues that are faced by the country. For example, Omanisation was a 
matter of concern. I wanted to look at projects that would help improve 
employment opportunities in Oman. We decided to look at franchising the business 
of lubricant service change and franchising of Select to young Omanis. And all 
this was supported by the senior management who believed in these ideas too.&quot;<br>
<br>
Faisal who was the Board secretary soon became a Director of the Shell Oman 
board. And as are with challenges and opportunities that seem to follow Faisal, 
he was offered yet another role. &quot;The new wing, Shell Representative Office, was 
recently set up in Oman and I was offered the position of the External Affairs 
Manager. Though the job was still with Shell, it was a different experience 
altogether. There were new challenges and new responsibilities. <br>
<br>
The office works closely with Shell Country Chairman in Oman (Dr. Andy Wood) and 
handles Shell’s interest and manages its reputation in the Sultanate.<br>
<br>
The Shell Representative Office also manages a social investment fund. One of 
the major concerns of Faisal and his team was how to ensure that projects are 
beneficial to society, address major concerns and most importantly are 
measureable. They invited some of Shell's major stakeholders and held a 
discussion about the subjects that needed to be addressed. &quot;During the 
discussion the feedback we received was to focus our programmes around 
education, Omanisation and employment,and health, safety and environment with 
focus around road safety. We were also asked that we should communicate openly 
and transparently about our programmes. Shell has taken on projects related to 
these fields through its social investment scheme,&quot; says Faisal.<br>
<br>
<b>Away from work</b><br>
All work and no play makes Jack a dull boy, an adage that agrees with Faisal. 
It's not just in his work life that he has gone in search of excitement and 
challenges but also personal life. &quot;I am a hobby hopping person. I have tried my 
hand at many interests, that include diving, tennis and hiking. But the one 
interest that has stayed on with me for the last 10 years is fishing. <br>
<br>
I really enjoy fishing. We go on an annual trip from the Muscat Game Fishing 
Association every year to Ras Al Hadd where we spend 3-4 days fishing,&quot; he says.<br>
<br>
Every weekend, Faisal sets off on his boat for fishing. &quot;If I come to office on 
Saturday without having gone for a fishing trip during the weekend, then it 
feels as though there has been no weekend for me, &quot; he says. And when he is not 
away indulging in his favourite hobby, you can catch Faisal relaxing at home on 
weekdays. </font></p>

