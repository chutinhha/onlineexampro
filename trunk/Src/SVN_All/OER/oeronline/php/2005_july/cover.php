                <font face="Verdana" size="1"><b>Pharming – latest Internet scam<br>
                </b>Pharming is the most recent Internet scam. It could turn out 
                to be more threatening as even experienced Internet users can 
                fall prey to it and may not know that they have been hit by an 
                Internet scam.<br>
                <br>
                Pharming, a play on ‘phishing’, involves highly skilled hackers 
                who secretly redirect user’s computer from financial sites to 
                the scammer’s fake ones, where they steal passwords and other 
                personal information. Even the Web address looks the same. The 
                process involves intercepting a user on his or her way to the 
                bank or credit card company. It can affect a large number of 
                Internet users at a time unlike phishing where a single user is 
                affected at a time. So far the hackers have been aiming at small 
                segment of the Internet and re-routing traffic to fake bank 
                sites to capture user passwords. The original legitimate sites 
                don't notice the drop in Web traffic, as pharming is restricted 
                to a small segment only. <br>
                <br>
                The scam is a recent phenomenon and Internet security gurus have 
                just begun warning Internet users about it. Pharmers follow two 
                different strategies – Attacking either user’s computer or the 
                large servers that find Websites for users. <br>
                <br>
                The first technique is to send virus-laden emails that secretly 
                install a software programme on the user’s computer. Whenever 
                the user makes an attempt to go to his bank's Website, he is 
                redirected by the browser to the pharmer’s fake site. The user 
                is then requested to update his information like login ids, 
                passwords or personal details like driving license number etc. 
                Even innocent users’ keystrokes are captured in order to steal 
                identities.<br>
                <br>
                The second way is to exploit the fact that Websites have verbal 
                names but reside at numeric addresses on the Internet. When 
                user’s type a Website's name into their browsers, Domain Name 
                System or DNS, servers read the name, look up its numeric 
                address and take users to the site. The pharmers meddles in by 
                changing the real site's numeric address to the fake site's 
                numeric address.<br>
                <br>
                This process called DNS poisoning is not a new phenomenon but 
                adopted recently by cyber criminals for stealing identities and 
                termed it as pharming.</font></td>
