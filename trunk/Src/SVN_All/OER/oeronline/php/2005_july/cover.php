            <p align="justify"><font face="Verdana" size="1"><b>Scams with Spams</b><br>
            Security scams such as phishing and pharming deceive earnest 
            customers to reveal their personal and financial information 
            cleverly. Phishing is an online Website identity theft. This sort of 
            Identity theft scam that ‘fishes’ for personal information start 
            with emails posing as legitimate emails from FBI, Ebay, PayPal, 
            Citibank, Visa or even our local banks. Phishing fraudsters use spam 
            (unsolicited emails) to direct Internet users to Websites that look 
            like legitimate e-commerce sites but are in fact, controlled by 
            crooks.<br>
            <br>
            A new scheme of so-called ‘pharming’ attacks is targeting the .com 
            Internet domain, redirecting some Internet users who are looking for 
            .com Websites to Webpages controlled by anonymous attackers. 
            Pharming attacks (typically through DNS hijacking or cache 
            poisoning) are similar to phishing identity theft attacks, but don't 
            require a ‘Web-link decoy’ that must be clicked by visitors to be 
            taken to the spurious Website. Most famous brand sites are favourite 
            targets as noted from the highlights from APWG- Anti-Phishing 
            Working Group.<br>
            <br>
            Spams are only a means to an end for the cyber criminals in their 
            mission. Users are asked to provide sensitive information such as 
            passwords, bank account information or credit card numbers, often 
            under the guise of updating an account. According to the APWG 
            reports, 13,141 unique phishing attacks were reported in April 2005 
            and this was a 180 per cent increase over the number of attacks 
            reported in March 2004, representing a monthly growth rate of over 
            75 per cent per month since December.<br>
            <br>
            If you have an e-mail account, you must have most certainly received 
            spam – unsolicited emails. In Internet terms, SPAM stands for ‘Self 
            Promotional Advertising Message’ while it also indicates ‘SPiced 
            hAM’ and unsolicited tinned food served by default. As per Gartner’s 
            research, when each day an employee spends an average of 49 minutes 
            in managing his mails, the negative impact of spam on workplace 
            productivity is significant. Since 1992, the spam mails scanned by 
            MessageLabs, the global leader in email security services, has risen 
            to 72 per cent in 2004. <br>
            <br>
            Around 14.5 billion spam messages are being sent around the world 
            each day. The cost to US organisations for fighting spam is 
            estimated at $17 billion (RO6.54 billion) in 2005. For some people, 
            receiving spam just means having to delete unwanted messages, 
            occasionally others suffer when their email-accounts are potentially 
            unusable. Internet service providers provide e-mail-filtering 
            services, which may be set to quarantine spam. Spam filters use a 
            statistical analysis of keywords within an email. Bayesian filters 
            calculate the probability of a message being spam. <br>
            <br>
            The problem is that identifying spam is not easy. SPAM filters 
            installed in the servers require consumer support fine-tuning that 
            can identify subsequent spam mails. Aggressive filtering rules mean 
            blocking 99.9 per cent of spam, but it would also block legitimate 
            mails.<br>
            <br>
            IBM, the industry leader, has very recently released FairUCE (Fair 
            Use of Unsolicited Commercial Email) product that uses identity 
            management features to link inbound e-mail back to its original IP 
            address by establishing a connection between an e-mail message, the 
            Internet domain and the computer from which the e-mail was sent. 
            FairUCE software runs on e-mail servers. It pulls IP addresses out 
            of e-mail messages and then compares those against one or more 
            databases of known spammers.<br>
            <br>
            Companies are motivated to invest in comprehensive Intruder 
            Detections and Prevention (ID/IP) security tools in an attempt to 
            strengthen consumer confidence in online transactions. Enterprises 
            must take several integrated security and awareness measures to 
            counter phishing and other cyber crime attacks.<br>
            <br>
            <b>Business Cautions</b><br>
            Most businesses looking to harness technology into core business 
            processes must be proactively responsive of the need for data 
            security. Lack of security is only marginally better than no 
            security. In case of security breaches, inform competent legal 
            authorities. Also inform your potential business partners and 
            clients of the potential risk of identity theft. Take all measures 
            to stop further access to sensitive data and seal loopholes in your 
            business security systems. Alter all admin passwords.<br>
            <br>
            When the compromise could result in harm to a person or business, 
            call your local police department immediately. Report your situation 
            and the potential risk for identity theft. The sooner law 
            enforcement learns about the theft, the more effective they can be. 
            Assign a contact person within your organisation for releasing 
            information. Give the contact person the latest information about 
            the breach, your response, and how individuals should respond.<br>
            <br>
            In case of breach of security and resulting leak of personal data 
            through your Website, immediately withdraw your site. Make it a 
            point of action to inform search engines and ISPs (Internet Service 
            Providers) to delete any such personal data from their server-cache 
            memories. </font></p>
