            <p align="justify"><font face="Verdana" size="1"><b>Data Brokers</b><br>
            Only after the recent security breaches leading to the compromise of 
            the privacy of thousands of naïve public, and the resulting media 
            publicity most people are aware of the so called ‘data aggregators’ 
            or ‘data brokers’ or ‘information brokers’. These companies compile 
            in-depth information about almost everyone, even though many have 
            never heard of them, have never had an account with them, nor have 
            given them permission to obtain personal information .<br>
            <br>
            Such firms collect and store information about the common public 
            from different sources including public records (registration 
            systems, court records, property tax assessment files, medical 
            examination reports, professional practice licenses, vehicle 
            registration, etc), along with credit reports and demographic data. 
            Consequently, they sell this information to their customers who may 
            be employers, loan collectors, media organisations, law offices, 
            enforcement officials, landlord or insurer. <br>
            <br>
            Unaware of this access, the customer may apply for a loan or 
            insurance claim. To their dismay, they may be turned down based on 
            the individual’s enigmatic background check. The situation gets 
            worse if the criminals having access to sensitive data find it easy 
            to impersonate innocent people for their criminal activities. The 
            naïve apart from losing their credit also lose their identity. When 
            law enforcement acts upon this criminal data, the poor victim has no 
            defence having lost his identity.<br>
            <br>
            ChoicePoint is one of the largest data aggregators in the US, 
            holding about 19 billion files of public information. Some of this 
            belongs to employees of the government, defence personnel and even 
            senators. The identity thieves who obtained ChoicePoint accounts 
            through the establishment of fake business identities could then 
            have online access to ChoicePoint’s data files; to obtain all the 
            personal information they needed – including Social Security numbers 
            and date of birth – to successfully commit identity theft. <br>
            <br>
            Yet another leading data broker, the Reed Elsevier Group’s 
            LexisNexis unit, announced in April this year that personal 
            information on 310,000 US citizens may have been stolen. It seemed 
            that its databases had been fraudulently breached 59 times using 
            stolen passwords, leading to the possible theft of personal 
            information such as addresses and Social Security numbers. <br>
            <br>
            <b>Security Negligence</b><br>
            There has been a rapid erosion of privacy rights due to faulty data 
            security that certainly calls for timely actions. In March this 
            year, on a Business Week forum, a hacker named ‘brookbond’ 
            penetrated an Admission System and posted instructions on the 
            Internet for applicants to know their application status before 
            being formally notified by the respective colleges. <br>
            <br>
            About 400 colleges and universities use this admissions management 
            system, hosted and managed by ‘ApplyYourself’ of Virginia. Harvard, 
            MIT's Sloan School of Management and business schools at Dartmouth 
            College, Duke University and Stanford University were among those 
            universities that were affected.<br>
            <br>
            When the Bank of America lost computer tapes containing credit card 
            records of US senators and more than a million US government 
            employees, they simply put the customers at increased risk of 
            identity theft. Social Security numbers, addresses and account 
            numbers were on the tapes for 1.2 million account holders (about 
            900,000 of which belonged to US Department of Defence employees). 
            Bank of America routinely ships backup data tapes for storage at 
            different locations in case any of their office is damaged by fire 
            or flood. This only exposes that the vulnerability attacks are at 
            the weakest security link in an IT-based system. Lack of diligence 
            in handling data results in victimisation of innocent consumers. It 
            warrants certain responsible action by the company involved.<br>
            <br>
            <b>Art of Balance</b><br>
            Lawmakers are now concerned about criminals gaining access to 
            thousands of consumer profiles in a database maintained by data 
            profiling companies. Until contemporary initiatives taken by the 
            legal forum of California and Texas, there were no legal bindings on 
            their business practices of data aggregation companies. 
            Nevertheless, most countries are currently looking into prohibiting 
            sale of national and social identity data for commercial purposes. 
            Any such law should stipulate that consumer information, including 
            credit reports, cannot be released to a third party without the 
            express permission of the consumer, a provision called ‘opt-in’. New 
            laws are needed to help data theft victims recover more rapidly. For 
            example, individuals must be able to change their National ID 
            numbers, if their numbers have been stolen and used to perpetrate 
            fraud.<br>
            <br>
            Considering the vital services provided by such information brokers 
            to the industry in general, it is not appropriate to cripple them 
            with strong regulations. However, they need to bear more liability 
            when security breaches occur and protect the consumers. The effort 
            to inform consumers about security breaches that they have 
            experienced and thus might expose consumer data is mandatory. 
            Encryption of data where possible, must be performed more diligently 
            on entities that can access the data and stringent access controls 
            should be enforced.<br>
            <br>
            According to California’s privacy notification law, citizens can now 
            exercise a ‘security freeze’ on their credit file in order to 
            prevent identity theft. It means that your file cannot be shared 
            with potential creditors without your explicit consent. When 
            businesses need to open credit accounts, they shall consult the 
            credit history. In such a case, the credit freeze will disable any 
            impostor producing your credentials and thus using your identity .
            <br>
            <br>
            It is now in effect a matter of striking the right balance between 
            nourishing online trades and controlling the trade practices. The 
            information brokers play an important role in the business practices 
            by maintaining up-to-date status of financial information about 
            their clients. Such information when authorised by the client can be 
            accessed by banks for processing loans, or verified by landlords for 
            effecting rental contracts. Insurance companies may also vie to 
            access this information prior to offering policies and claims. But 
            they must be enabled access to the data only upon due permission 
            from their clients to view such credit history.</font></p>
