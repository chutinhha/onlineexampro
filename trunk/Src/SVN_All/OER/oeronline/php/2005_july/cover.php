            <p align="justify"><font face="Verdana" size="1"><b>Opportunities 
            for ID Theft</b><br>
            Identity criminals have devised many scrupulous schemes to gain 
            access to people’s personal information. They get information from 
            businesses by stealing records from their employer or bribing an 
            employee who has access to these records or by hacking into the 
            company’s network and computers. Alternatively they search through 
            businesses’ trash (dumpster diving) . <br>
            <br>
            Sometimes, it is possible to obtain credit reports by impersonating 
            as one who may have a right to the information. It is even to steal 
            credit and debit card numbers while using it on legitimate 
            operations (skimming). Stolen mail with bank and credit card 
            statements and cheques reveal vital information occasionally. Such 
            mails could even be completely diverted to another location by 
            change-of-address facility. Information can simply be stolen from 
            your home or you can even be conned by someone posing as a 
            legitimate businessperson or government official. Identity theft 
            online can happen due to security threats such as ‘hacking’, 
            ‘spamming’, ‘spyware’, ‘phishing’ or even ‘pharming’.<br>
            <b><br>
            Consequences of ID Theft</b><br>
            Once identity thieves have your personal information, they may 
            misuse this in different ways. Normally, credit or debit cards when 
            stolen result in immediate heavy shopping bills being swiped through 
            or ATM misuses (most often, PIN number scribbled in the card’s 
            encasing envelope are responsible for this). Personal details, 
            including name, date of birth and ID or passport number may be 
            sufficient to open credit card accounts. Most financial institutions 
            offer mail change facilities to their cardholders. This facility can 
            be misused to divert account statements. In this type of fraud, the 
            owner may take some time to know there is a persistent problem of 
            unsettled credit card bills.<br>
            <br>
            Loans such as automobile loans and phone or Internet connections can 
            be availed based on basic identity details along with passport 
            photographs. It may even be possible for fraudsters to open a bank 
            account in a stolen name and write bad cheques on that account. If 
            it is alarming to even think of such possibilities, consider the 
            cases of naïve victims suffering with lost identities that have been 
            misused.</font></p>
