            <b>Be Aware</b><br>
            Identity theft is an issue of consumer and business awareness. Most 
            often, when you are residing in Oman, enter details on a form (say 
            application, registration, request for service, order products, 
            etc.) the information becomes part of a database that’s used by 
            businesses later. But the late 90s has seen a new set of criminals 
            stealing identities using electronic means that seems a lot easier 
            to do.<br>
            <br>
            Your everyday transactions, which usually reveal bits of your 
            personal information: your bank and credit card account numbers, 
            your income, your National ID/Labour Card number or your name, 
            address, and phone numbers. An identity thief obtains some piece of 
            your sensitive information—say personal or financial—and can use it 
            without your knowledge to commit fraud or theft.<br>
            <br>
            Identity theft is a serious crime. People whose identities have been 
            stolen can spend months or years and their hard earned money in 
            cleaning up the mess the thieves have made of their good name and 
            credit record. Some victims have to pay a very heavy penalty as they 
            lose job opportunities, get refused loans for education, housing or 
            cars, or even get arrested for crimes they didn’t commit.</font></p>
