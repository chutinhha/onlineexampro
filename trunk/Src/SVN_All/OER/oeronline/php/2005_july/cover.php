            <p align="justify"><font face="Verdana" size="1"><b>Consumer 
            Prudence</b><br>
            Prudential measures on behalf of the consumers certainly curtail the 
            vulnerability of security while also minimising the extent of damage 
            in the event of ID theft. Account holders must solicit periodical 
            transaction statements and monitor the transactions and balances 
            carefully. <br>
            <br>
            In case no credit bills are received, seek clarification from 
            competent authorities. There have been instances of online customers 
            from Oman who have suffered from misuse of credit card numbers due 
            to which they had to pay large bills run up by deceptive criminals. 
            In case of stolen wallets or identity cards, inform the bank 
            officials and police officials immediately to stop accepting them 
            for processing at any outlets. Place effective passwords on your 
            credit card, bank and phone accounts.<br>
            <br>
            Do not give out personal information on the phone, through the mail, 
            or over the Internet unless you have initiated the contact or are 
            sure you know whom you are dealing with. Identity thieves can be 
            skilled liars, and may pose as representatives of banks, Internet 
            service providers (ISPs), or even government agencies to get you to 
            reveal identifying information. Before you divulge any personal 
            information, confirm that you are dealing with a legitimate 
            representative of a legitimate organisation.<br>
            <br>
            If your cheques have been stolen or misused, close the concerned 
            bank account after duly informing them. Most banks are responsible 
            for losses from a forged cheque, but they also require your 
            cooperation in taking reasonable care of your account. The liability 
            of loss may fall upon the consumer if in case he fails to inform the 
            bank in a timely manner of the loss or theft. Contact your banking 
            or other financial institutions for further details. Before 
            revealing any identifying information (for example, on an 
            application), ask how it will be used and secured, and whether it 
            will be shared with others. If possible, choose to keep it 
            confidential.<br>
            <br>
            Limit the identification information and the number of credit and 
            debit cards that you carry to what you’ll actually need. Avoid using 
            an automatic login feature that saves your username and password 
            while protecting financial information on laptops. Delete any 
            personal information stored on your computer before you dispose of 
            it. Use a ‘disk wipe’ utility program, which overwrites the entire 
            hard drive and makes the files unrecoverable.<br>
            <br>
            Transact only with those Websites that have secure privacy policies 
            answering questions about the access to and accuracy, security, and 
            control of personal information the site collects, as well as how 
            sensitive information will be used, and whether it will be provided 
            to third parties.<br>
            <br>
            <b>Final Word</b><br>
            The law enforcement officials across the nation need to help 
            customers and businesses to stop identity thieves by providing easy 
            access channels of communication, information dissemination and 
            resolution. Some such simple mechanisms are toll-free telephone 
            lines, customer call centres for technical clarifications, Web-based 
            complaint submission facility, SMS for immediate attention towards 
            malicious websites and loss of identity.<br>
            <br>
            Competition and brand image is pushing companies to adopt new IT 
            security tools in a bid to strengthen consumer confidence in their 
            online transactions. Financial institutions enabling user 
            interaction through electronic means, say tele-banking, online 
            payments and Web-based enquiries are more compelled for investment 
            in security and it is indeed a matter of survival through testing 
            technical counter- measures.<br>
            <br>
            Consider the initiative of ETrade Financial Corporation when it 
            announced that it is offering high-value customers a token-based 
            form of authentication technology that can be used with passwords to 
            provide an additional layer of protection for accessing online 
            accounts. Other potential measures such as biometric authentication 
            using fingerprint templates stored in Oman’s national identity cards 
            may enhance physical identity verification beyond factual data.<br>
            <br>
            According to research by information security company Vontu, 95 per 
            cent of data security risk comes from faulty business processes and 
            untrained users. Creation of awareness about security 
            vulnerabilities is a resilient preventive measure. Omani banks are 
            truly concerned about their customers and as a reflection of their 
            concerns, issue alert notices time and again. <br>
            <br>
            Awareness initiatives, for instance those taken by the Central Bank 
            of Oman in cautioning the public against increasing attempts of 
            fraud and BankMuscat issuing an advertisement regarding SPAM mails 
            are certainly praiseworthy. Adoption of the National ID card 
            information into mainstream activities surrounding the life of a 
            citizen will enable physical verification of identity. <br>
            <br>
            ID theft cannot be completely eliminated or completely controlled 
            like any other crime. But, according to the US - Federal Trade 
            Commission, one can minimise risk by managing personal information 
            cautiously and with heightened sensitivity considering the abuse of 
            their vulnerability. <br>
