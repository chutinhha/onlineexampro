</font><br>
<font face="Verdana" size="1"><b><i>OER</i> gets face2face with Alan Perkins, 
Sr. Regional Risk Manager – Middle East, Visa Intl CEMEA on the issues related 
to card frauds and measures taken to prevent them<br>
</b>
<br>
<b>
<img border="0" src="face_face.jpg" align="left" vspace="1" hspace="7">Welcome 
to Oman. Is this your first visit? </b><br>
No, I have come many times here before. It’s a great pleasure to be here. I have 
been based in the Middle East for the past five years and I keep coming here. 
It’s a fantastic place. To me this is a real friendly country in the Middle 
East. <br>
<br>
<b>How prevalent are card frauds? </b><br>
If you look at frauds in terms of the proportion of card sales then the 
proportions are going down, but the problem is that the dollar value is going 
up. The card fraud rate in Oman is traditionally lower than other parts of the 
region. I think some of the banks here have been very proactive and invested in 
good risk management teams, instructors and employees. They have invested in 
fraud management tools and equipments. The number one advantage they have got is 
perhaps the best and the most prioritised police principles in this area of 
expertise in the region. <br>
<br>
Oman has made legislation changes to deal with the issue. So now their 
legislation covers Internet fraud, payment theft fraud and financial crime in 
general. The officers are all well trained about the legislation. On our part, 
we are working hard on preventing card frauds and trying to ensure that the 
banks are aware of the issues involved. So they don’t have major losses in the 
future, it’s better to act now and try and cut the losses and problems that have 
been happening rather than reacting after the mishap. <br>
<br>
<b>So what steps are you taking to minimise risks?<br>
</b>We have been asking banks to monitor card transactions round the clock. The 
application of chip technology has reduced card fraud rates in countries that 
are upgrading to a chip-based payment infrastructure. We have been advocating 
the increased usage of chips in the Middle East though it will take some before 
it gets popular in this part of the world.<br>
<br>
Apart from conversion to chip, we’ve started constant interaction between banks 
to facilitate open discussions on frauds between them. See, you can’t look at 
frauds as a competitive issue. A criminal doesn’t turn up in Oman and say I only 
want to target the National Bank of Oman! Everybody is going to get hit so we 
better try to co-operate with each other. We are assisting the banks in 
establishing contacts with merchant tie-ups to address the issue. Good banks 
including Oman International Bank and BankMuscat have organised training 
seminars with their key merchants and they’ve invited Visa and Mastercard to 
come in and assist them with their training. Just basic fraud awareness, 
security features of cards and customer behaviour sort of training is what we 
are looking at.<br>
<br>
We are working closely with the police not only to help in the arrests in the 
first place but once the arrests are made, to gather the evidence if they 
require to successfully prosecute the criminals. Some spectacular arrests have 
been carried out here in Oman in the last 2-3 years. At the moment, the 
relationship with police is at an exceptionally high level. Take the example of 
the initiative of a conference of Visa and Mastercard, co-ordinated by police, 
to give some hard-core training to judges, prosecutors, attorney general office; 
it never happened anywhere else. I don’t know of this happening in mature 
markets in the world. It’s a fantastic achievement of the Oman Police. It’s 
because of this high level of co-operation that some of the card fraud criminals 
are locked up in Oman, and not in Dubai or Sharjah. <br>
<br>
<b>Talking of the Middle East, what are the lessons for banks? </b><br>
At the moment, the card frauds in the Middle East are not very high. But that 
may not be the scenario in the near future with governments trying to 
aggressively promote tourism and opening up borders of control. So it becomes 
easier for people to come in. Ok, Saudi and Kuwait are still very stringent but 
Dubai is an open market and Oman is going that way as well. So there is more 
likelihood for criminals to come in and exploit the payment cards.<br>
<br>
Dubai is a huge target market at the moment. Muscat too with its peaceful, 
friendly and prospective business environment, attracts everyone. But look at 
these markets from the angle of card frauds. We’ve had major cases where 
criminals have flown in here and got passports stamped to go to Qatar, so just 
when police have tracked them down in Qatar, they are back on the plane to Oman. 
So it’s becoming easier for criminals to move fluidly across borders. The major 
problem is that the cross border crime is the hardest one to track and 
prosecute. <br>
<br>
The banks have to accept this fact that they can minimise the card frauds with 
constant monitoring of transactions. It can’t be a zero fraud rate as fraud is 
going to take place somewhere in the world. Let’s get the tools in place and try 
to stop it rather than blaming each other that who is liable for it after the 
event. That doesn’t take you anywhere. <br>
<br>
<b>How does the police get aware of the new ways of fraud?</b><br>
Police invites us on a regular basis to give them training on latest issues, for 
example, in Muscat in the last year or so, there has been an increase in the 
hacking cases. The criminals send e-mails supposedly sent from banks asking you 
to fill in your card number, your pin number etc. on the pretext of verifying 
your account. Your bank knows your card and pin number. Why should the bank be 
doing it again? The customer and the police need to know this. <br>
<br>
The police invite us to talk and address them on all these types of cases and 
give them some training. They are very keen and are fully aware of the latest 
fraud credits. The other alternative is that we arrange for sending their few 
officers over to the UK. As their English is weak, they will stay with a family 
in the UK and learn English as a foreign language. It will expand their 
experience and knowledge because most of the news articles in this area of 
business tend to be in English. You look up the Internet for crime and 
investigation related developments. So we encourage them to go and improve their 
English. <br>
<br>
<b>So is there a role for any other Government agency on this issue?</b><br>
Certainly, we deal with the Central Bank of Oman on this. It’s obviously 
important that they play an important role as they can exercise the required 
control, bring in changes which need to be done and assist wherever and whenever 
necessitated.<br>
<br>
<b>How does this work best if the Central Bank gives all the banks to follow 
certain set of guidelines or the banks themselves come up with certain 
awareness? What works better? </b><br>
I think it’s a combination of the two. We’ve got a couple of countries in the 
Middle East where the central bank dictates. For example, in Saudi Arabia, 
everything is controlled by Saudi Arabian Monetary Agency. In places like here, 
it is a bit more relaxed. When there are particular problems like ATM frauds 
which hit Dubai couple of years back, the Central Bank dictated that certain 
types of ATMs should be taken out. That’s a very confusing message because ATM 
fraud can happen in any design of machine! So it has to be a partnership rather 
than going separate ways. <br>
<br>
The other one is the migration to chip. That’s got to be worked together. 
There’s no point for banks to come to Central Bank and say we worked with those 
standards but now we’re not complying with Visa standards or Master-card 
standards. It all ought to be one and the same. Let’s have one common type of a 
chip with a common functionality that everybody can understand and work with. 
And that’s where we’re coming from. We are not a competitor. We don’t want to 
dictate anybody about what the chip does, let’s work together and make sure that 
it will work smoothly. <br>
<br>
<b>But do you think that’s going to happen?</b><br>
I don’t see any reason why it shouldn’t! Some countries are always going to be 
harder to work than others. In most of the Middle East countries, I don’t see if 
there is going to be any particular problem with that. <br>
<br>
<b>Ok. Going by what you say, does it also mean that Visa and Master are coming 
together and saying it’s a one chip?<br>
</b>Yes, it’s an industry standard. <br>
<br>
<b>Moving to technology aspects, what are the new areas which need to be looked 
at?</b><br>
Let’s take Internet frauds. There is a perception that it is dangerous to do 
transactions on the Internet because your data is going to be stolen; your card 
account will be compromised. Now whether or not that perception is correct is 
another matter but the issue here is the verification method. The bank and the 
merchant establishment want to verify that it’s actually you who is using the 
card and it’s not somebody else who is using your card. Similarly, the user 
(customer) wants to know and the bank wants to know that it’s a genuine merchant 
establishment.<br>
<br>
While doing a transaction, you would be asked a security question which you have 
as a genuine customer must have given to the bank which only you know. For 
example, what colour underpants do you wear? Now how does a criminal know? He 
starts guessing. It could be any question like that, anything bizarre like that. 
Now I am a criminal and I got your card and I try to use it. Up comes the 
question, what colour underpants do you wear. I put in black, it comes invalid. 
It won’t go past that stage; it won’t go to the bank for authorization. It will 
just stop. Similarly, we need to know this is a genuine merchant because as we 
all know, there have been occasions where spoof merchants have been set up with 
a shadow website to try and get account data. Once the two parties have been 
verified, the transaction is likely to go through. <br>
<br>
In other markets like USA or UK, that perception isn’t there. Talking of Middle 
East, I certainly believe that with the younger generation, the scenario is 
going to change. The younger Omanis study and travel abroad, and they are more 
exposed to all these types of methods of communications. When was the last time 
that we sat down and wrote a letter? I can’t remember! The instantaneous nature 
of the medium makes it almost make people demand it! <br>
<br>
<b>What would be your advice to businesses and individuals in the region on how 
to first detect and then prevent? </b><br>
First and foremost there has to be an appreciation that this is the way the 
world is going. The world is moving away from cash based transactions. I think 
what we have to concentrate on is to grow the card industry. But we don’t want 
to see rise in debt and non-payments like we see in UK where personal debt is 
growing because credit is being allowed too easily.<br>
<br>
So let’s combine it with good cardholder education in terms of personal finance; 
in terms of security; in terms of right to protect themselves and their 
finances. Now that’s the first aspect. Now the second aspect I would say that 
there shouldn’t be this fear of card transactions or Internet transactions. Let 
me give you a scenario, if you were going to Johannesburg tonight, would you be 
carrying a couple of thousand dollars in cash, or would you prefer to have a 
card with you? Again the commonsense is there is more risk with cash than card. 
If you lose the cash, you lost it; it’s period. <br>
<br>
You got countries like Iceland where there is virtually no cash. It’s a cashless 
society, everything is done on plastic. That’s how we perceive the future to be 
going, how fast we get there is another issue. Perhaps this part of the world is 
not going to go as fast as the other countries in the world but with the opening 
up with the tourism, it will make it faster.</font></p>

