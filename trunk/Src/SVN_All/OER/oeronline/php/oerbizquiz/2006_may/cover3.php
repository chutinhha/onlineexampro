            <br>
            BANKING benchmark</font><font face="Verdana" size="1"><b><br>
            Although it has slipped to No. 3 from the previous year’s No. 2, 
            BankMuscat continues to cling on to its leadership in the banking 
            industry</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt; font-weight: 700">B</font><font face="Verdana" size="1">ankMuscat 
            Chairman AbdulMalik bin Abdullah Al-Khalili is a happy man. And, why 
            not! There are reasons for him to feel being on Cloud Nine. His bank 
            continues to be the leader in the Sultanate, enjoying a 37.83 per 
            cent market share in terms of total assets, 37.92 per cent in terms 
            of total credit, and 34.9 per cent in terms of total customer 
            deposits as at 31 December 2005. Above all, he has walked away with 
            the glory of being the first Gulf Cooperation Council (GCC) bank to 
            take the Global Depository Receipts route and get listed on the 
            prestigious London Stock Exchange.<br>
            <br>
            <img border="0" src="bankmuscat1.jpg" width="200" height="251" align="left" hspace="5">Yet, 
            in the race to for the top slot of Top 20 Omani corporates, the 
            juggernaut of the unstoppable Omantel has railroaded BankMuscat one 
            perch down from its previous level. Net result: BankMuscat now 
            occupies the Number 3 slot for the year 2005 as against Number 2 in 
            the previous. One more thing: BankMuscat’s performance has been so 
            well orchestrated that Financial Times of London, The Banker and 
            Euromoney cannot deny anointing it as the ‘Best Bank in Oman’ for 
            the 5th year running.<br>
            <b><br>
            Bottom line</b><br>
            When curtains came down on fiscal 2005, BankMuscat had posted a net 
            profit of RO 45.4 million — up by 33.1 per cent. Its net interest 
            income gained 3.9 per cent to close with RO 78.1 million. 
            Non-interest income gained 6.5 per cent during the period under 
            review. Analysts at Fincorp point out that “operating cost as a 
            percentage of net operating income has come down to 39.3 per cent 
            from 40.5 per cent in 2004, signaling more efficiency.” <br>
            <br>
            <b>Business</b><br>
            Preparations to commence operations in the Kingdom of Saudi Arabia 
            and the United Arab Emirates are on in full swing. It has already 
            obtained permission to open branches in Qatar and Kuwait. Continuing 
            its assistance in nation building activity, BankMuscat’s corporate 
            banking division played a key role in almost all projects of 
            national importance by unzipping its purse strings for projects in 
            the oil and gas, power, telecom, logistics, agriculture and tourism 
            sectors. Sensing a new growth opportunity in the small and medium 
            enterprises (SME) segment, it has introduced Al Wathbah. As far as 
            managing mandates are concerned, BankMuscat is advising the Oman 
            government on the privatization of its first independent water 
            project, the Sharqiyah Independent Water project (IWP), the 
            privatization and restructuring of solid waste management in the 
            country, and the advisory and fund raising mandate for a leading 
            petrochemical project in the Sultanate. <br>
            <br>
            Net loans and advances portfolio grew to RO 1,375 million — up by RO 
            43 million between 2004 and 2005. Net loans and advances shot up by 
            12.9 per cent. Customer deposits leapt from RO 1,162 million in 2004 
            to RO 1,333 million in 2005. Savings deposits — including the 
            popular Al Mazyona scheme — increased by 17.7 per cent to RO 338 
            million in 2005 — up by RO 51 million. As far as loan loss is 
            concerned, it increased to Rs.24.4 million from RO 20.4 million in 
            2004. Loan recovery gained momentum in 2005: it recovered RO 15.9 
            million in 2005 as against RO 6.3 million last year. Besides, it 
            also recovered RO 1.9 million from provision for placements in 2005. 
            Bancassurance is gaining ground, but will take time to make its 
            presence felt on its balance sheet. Investment Banking division 
            handled IPOs of AES Barka, Dhofar Power and Taageer Finance 
            successfully. The most talked about Omantel IPO was lead managed by 
            BankMuscat. <br>
            <br>
            The Private Banking division has begun to focus on the high net 
            worth individuals while the Asset Management Division was also 
            witness to considerable activity: the erstwhile Oryx JIA fund rolled 
            over into the new Oryx and brought in Aberdeen Asset Management as 
            investment advisor. Muscat Index Fund rolled over into the Muscat 
            Fund and switched from close-ended to open-ended structure. What 
            about its risk taking ability? Four global rating agencies — Fitch, 
            S&amp;P, CI and Moody’s gave the highest ranking to BankMuscat. What 
            more can one ask for? For 2005, BankMuscat declared a cash dividend 
            of 35 per cent and a stock dividend of 10 per cent, subject to 
            stakeholder and regulatory approvals.<br>
            <br>
            <b>Challenges</b><br>
            Chairman Sheikh AbdulMalik points out that the year 2006 promises 
            “to be the more exciting for three developments that will impact the 
            banking sector” directly and indirectly: the advent of Free Trade 
            Agreement with the United States, World Trade Organisation as well 
            as Basel II regulations being implemented, are all expected to set 
            the course for BankMuscat. WTO and FTA norms will throw up more 
            business opportunities, with Oman industries gearing up to face the 
            global competition and the possible entry of foreign companies into 
            the Sultanate. <br>
            <br>
            BankMuscat is geared up to respond to the “shifting conditions”. 
            Rightly, the focus from 2006 onwards will be on developing expertise 
            in fee and commission based operations to cushion any fall in the 
            conventional interest income. Moreover, the entry of more banks — 
            Bank Sohar and Bank of Beirut — in the near future and the resurgent 
            National Bank of Oman is likely to keep BankMuscat on its toes.
