            <font face="Verdana" style="font-size: 16pt"><b>
            RISING 
            STAR</b></font><font face="Verdana" size="1"><b><br>
            Powered by another year of 68 per cent growth in income, Oman Cables 
            has moved from the position of No.10 in 2004 to No. 8 in 2005</b></font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" style="font-size: 11pt"><b>
            I</b></font><font face="Verdana" size="1">n 
            the OER Top 20 rankings last year, Oman Cables Industry (OCI) had 
            muscled its way to No.10, moving up four notches from its 14th 
            position in 2003. This year, the company has bettered its 
            performance of last year by grabbing the No.8 position. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="omancable1.jpg" width="200" height="270" align="left" hspace="5" vspace="2"></b></font><font face="Verdana" size="1">Part of the cable industry, OCI mainly caters to the segment of 
            cables and conductors required for transmission and distribution of 
            electric power and transmission of signal for instruments. This 
            industry is directly linked and dependent upon the demand and supply 
            of electric power generation, transmission, distribution, 
            infrastructure development and new industrial projects. In terms of 
            market environment, OCI had to weather the continuous increase in 
            prices of almost all raw materials, the worldwide phenomenon, 
            including copper, aluminium, steel as well as prices of insulating 
            and sheathing compounds. This has naturally affected the selling 
            prices.<br>
            <br>
            “In 2005, OCI, as in the year before, delivered a record performance 
            across several milestones — corporate management, operational 
            outputs, financial discipline, strategic investments in 
            manufacturing and processes, and formulating robust business plans 
            and sound practices that adapt rapidly to changing market 
            environment with synergy and vitality, supported by invaluable 
            contributions from each and every one of our employees,” exudes 
            Mustafa Mukhtar Ali Al Lawati, Chairman of OCI. “All this has 
            collectively resulted in our total business performing exceedingly 
            well,” he adds.<br>
            <br>
            Production output increased significantly. OCI started producing 
            medium voltage (MV) cables with in-house expertise. These cables 
            have been marketed successfully, both regionally and 
            internationally. The company‘s total sales increased to RO 60.577 
            million in 2005, compared to RO 36.002 million in 2004, registering 
            a growth of 68.3 per cent. <br>
            The profitability (profit after tax) has also grown significantly by 
            90 per cent over 2004. This increase could be attributed to factors 
            like increase in production capacity for various cable products, 
            such as low voltage cables, medium voltage cables and over head 
            transmission line conductors of varying voltages, increase in plant 
            productivity, and increase in price of raw materials. The company 
            proposed a dividend of 20 per cent.<br>
            <br>
            OCI has sound internal control system based on the backbone of ISO 
            9000-2000 and various internal operating and management manuals.<br>
            <b><br>
            Relationship with Draka Holdings, Netherlands</b><br>
            OCI and Draka arrived at an amicable arrangement for strengthening 
            mutual relationship to expand beneficial cooperation for the future. 
            OCI accepted Draka’s offer of a management contract to manage 
            Draka’s subsidiary cable manufacturing company in India, Associated 
            Cables Private Limited (ACPL). A monthly fee, stake in ACPL equity 
            and annual monetary payments to OCI has been agreed. The relevant 
            agreements, giving effect to this arrangement have been signed and 
            are in the final stages of implementation. <br>
            <b><br>
            Growth outlook</b><br>
            The demand for electric power in the markets where OCI operates is 
            growing. Economic activity in the region is on an upswing; 
            investment in power generation is on the up; and many mega projects 
            are being executed. This will translate into demand for electrical 
            power and thereby, conductors and cables. The Omani market growth 
            being slower than the region, OCI’s dependence on export is likely 
            to increase, which has various implications.<br>
            <br>
            OCI is strengthening its market presence by opening offices in 
            important markets. It has commissioned state-of-the-art PVC 
            compounding plant which is the largest in Asia and the fifth of its 
            kind in the world. This is the company’s first step in backward 
            integration as a growth strategy to stay competitive.<br>
            <br>
            “In view of the non-availability of land for OCI’s future expansion 
            at the present site, OCI has acquired land measuring about 92,000 
            sq. m. in the Rusayl Industrial Estate, just about a km from the 
            present plant. Such large area was available only in the hilly area, 
            on the fringe of the industrial estate, for the expansion project,” 
            says Mustafa Mukhtar Ali Al Lawati, Chairman of OCI.<br>
            <br>
            “Thanks to its proximity and convenient access, it will provide 
            substantial cost benefits. The second plant and necessary open space 
            around the new plot, entailed considerable cutting of hill and 
            leveling of the land, before the new plant could be constructed and 
            completed,” he adds.<br>
            <br>
            It is the biggest expansion that OCI has undertaken so far. It 
            involves an investment of around US$13 million. With this facility, 
            OCI is expected to meet its customers’ cable needs across the global 
            market, in the future.</font></p>
