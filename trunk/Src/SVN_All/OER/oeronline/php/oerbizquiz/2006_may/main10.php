<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<b>Impact: </b>It is encouraging to see that Oman is working hard towards 
expanding its non-oil industrial base. The Government is taking effective steps 
to encourage foreign investors to invest in the large-scale gas-based projects. 
In order to further diversify the economic base of the country, the Oman 
government has taken a large number of initiatives to promote the country as one 
of the leading tourist destinations in the region. The announcement of mega 
projects such as ‘The Wave’ and ‘Blue City’ are already showing positive signs 
on the economy. The Oman government is striving hard to reduce its dependence on 
oil revenues, though it still remains a major part of the government revenue. 
Therefore, oil prices will remain a sensitive issue for the economic outlook for 
the country.<br>
<b><br>
Going Forward...</b><br>
There is no doubt that from the regulatory and valuation perspective, Oman still 
has a lot of attraction for its investors, both at the regional and domestic 
levels. In terms of P/E, Oman is the second best in the region. The MSM, as on 
March 30 prices, is trading at 12.85; the only other market better than Oman is 
Bahrain, which is trading at a P/E of 9.88.<br>
<br>
However, it would be prudent to keep in mind the above-mentioned factors while 
making a final call on the market. In all probability, it would be difficult for 
MSM 30 to maintain the performance of previous years in 2006. Investors should 
taper down the level of expectations from the market. A growth of 10-15 per cent 
during the year would be a more realistic target. Sector and stock selection 
will play a pivotal role in determining the final performance of investments in 
MSM. The banking sector is still attractive. However, one has to be more careful 
in selecting the banks. BankMuscat is the pick of the local banks, thanks to its 
strong balance sheet and a proactive approach of diversifying its business by 
opening its branches in Bahrain and India. The bank intends to open branches in 
Qatar and Kuwait as well.<br>
<br>
Companies carrying the potential to directly benefit from the enhanced level of 
economic activity in the country should be looked at favorably. The cement 
sector will be one of the leading sectors. Raysut Cement, thanks to its expanded 
capacity, is an ideal pick. In addition, companies related to construction and 
contracting activities can also be part of the portfolio. Utilities and oil 
marketing companies also have value for the medium to long-term investors. <br>
<br>
To conclude, a more cautious and focused investment approach is required in 
managing a portfolio for the remaining part of the year. Timely profit, taking 
and using the cash in picking the select stocks at attractive valuation at a 
later stage, should be a part of strategy. Besides reducing volatility, 
diversification — through investing in different economies and asset classes — 
should also be done.<br>
<i><br>
Zaigham H. Shah is the Vice President — Asset Management of FINCORP.</i></font></p>

