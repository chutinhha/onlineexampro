<b>MSM: Will the momentum continue?</b></font><font face="Verdana" size="1"><br>
<b>A more cautious and focused investment approach is required in managing a 
portfolio for the remaining part of 2006, predicts Zaigham H. Shah </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="byinve.jpg" width="135" height="190" align="left" hspace="5" vspace="3">M</b></font><font face="Verdana" size="1">uscat 
Securities Market (MSM) has been one of the most rewarding equities markets in 
the Gulf Cooperation Council (GCC) region. The bullish trend that started in 
2002 continues at the first quarter of the current year. MSM is attractive not 
only for the returns, which it has given, but the way these returns have been 
given. MSM has clearly been—relatively though—one of the most stable markets in 
the region. In comparison, the other GCC markets have given phenomenal returns 
in the last few years but stability has remained a cause of concern in those 
markets. <br>
<b><br>
Performance of MSM (Index Growth) </b><br>
If one observes the performance of MSM since 2002, several factors have 
contributed to its continuous and stable performance — low interest rates, 
absence of relatively attractive asset class, establishment of Capital Markets 
Authority, introduction of code of corporate governance, corporate 
restructuring, money supply in the system, attractive valuations, healthy 
dividend yields from corporate, and encouraging corporate results.<br>
<br>
<br>
To a judicious investor, the main question is whether this bullish trend will 
continue in future. Let us look at some of the factors that have led to the 
bullish trend.<br>
<b><br>
<img border="0" src="byinve1.jpg" width="249" height="230" align="right" hspace="5" vspace="2">Interest Rate</b><br>
Traditionally, MSM has always been regarded as a market that offers an 
attractive dividend yield on a comparative basis. Lower interest rates in the 
past few years have further encouraged the investors. Currently, Oman still 
offers a dividend yield of around 4.26 per cent, which is the best in the 
region. The only other market above 4 per cent dividend yield is Bahrain, which 
offers 4.18 per cent. <br>
<br>
Let us look at the global interest rate scenario. Thanks to the policy of 
pegging of Omani Rial to the US Dollar, any movement in the interest rates in 
the US makes an impact at the domestic interest rate. The current increase in 
global interest rates has raised a few concerns. As on March 2006, the 1-Year 
London Inter Bank Offering Rate (Libor) is at 5.248 per cent. The last time the 
world saw this level of rates was in January 2001 where 1-Year Libor was at 
5.284 per cent. During this time the world economies continuously saw a downward 
pressure in the interest rate. The lowest level of 1-Year Libor was witnessed in 
June 2003 when it went down to 1.201 per cent. The interest level started rising 
from the beginning of 2005 and since then 1-Year Libor has gone up by 60 per 
cent! <br>
<br>
Impact: Banks in Oman play a major role in determining the direction of the 
stock market, as they carry a significant weightage in MSM 30 Index. The current 
rise in interest rates is not a very welcome sign for the domestic banks, as 
they have been enjoying a more than decent spread for the past few years. Under 
the current scenario, in order to achieve the same level of performance, banks 
have to restructure their loan portfolio and introduce innovative products. 
Besides, handling of mismatch will be the critical job for the banks. Companies 
from industrial and service&nbsp; sector will also feel the pressure, especially 
the companies where debt to equity ratio is high and the size of short-term loan 
is significant. Cost of borrowing will be their immediate concern.<br>
<br>
Maintenance of attractive dividend yield will be a tough challenge in the coming 
year for the companies, as the dividend yield of 4-5 per cent may no longer be 
attractive for the dividend yield-oriented investors. The pressure to increase 
yield will result in higher dividend payouts. The equity markets may come under 
pressure to perform.<br>
<b><br>
Real Estate Threat</b><br>
The emergence of real estate as an attractive asset class is another threat to 
MSM. The continuing increase in oil prices over the past few years has 
encouraged the governments in the region to pep up their economic foundation. 
Almost every government in the region is using the excess liquidity arising out 
of increased oil revenues to stimulate growth by launching mega infra-structural 
and development projects. This will give a big boost to the economy. One of the 
direct impacts of buoyancy in the economy is on the real estate market in the 
region. The boom in the real estate in Oman in particular, and all over the 
region in general, has been felt more significantly in the past two years. <br>
<br>
The Oman government has initiated a few mega projects. One of these is the 
US$1.5 billion ‘The Wave’ project that has attracted not only the locals but 
foreign investors as well. The prices of land in the capital and its 
neighborhood, in some cases, have gone up to 40 per cent in the past one year. 
Land price in Sohar, Sur and in other areas has also seen a sharp upward 
movement. And regional investors are also showing interest in these areas.<br>
Impact: With the relaxation in ownership allowed by many regional countries, 
real estate has emerged as a very attractive asset class now. The growth of real 
estate-related private equity funds is another indicator of investors’ interest. 
The boom in the real estate market has already forced the investors to shift 
their funds from other asset classes. Equity markets have so far not been 
affected significantly. However, the recent trends indicate that the shift of 
funds from equity market to real estate is on the up. The quantum of this shift 
may increase further if the equity markets do not meet the expectations of 
investors.<br>
<b><br>
The IPO Bandwagon</b><br>
In the late 1980’s, the Japanese authorities kindled a speculative mania by 
floating a telecom giant, NTT. In probably unconscious imitation, the Gulf 
states, in the past few years, have zealously promoted privatization and come 
out with Initial Public Offerings (IPOs) at very attractive prices. In the 
recent past, we have seen a stock climb up to 500 per cent on the opening day of 
its trading. Such events clearly manifest the irrational expectations and 
behavior of investors. In some cases, we have witnessed applications for new 
issues being oversubscribed up to 800 times. In my opinion, this was the first 
sign of caution.<br>
<br>
However, there are positive aspects of these IPOs. For instance, it has 
increased the depth of capital markets both in terms of increased number of 
companies listed on the stock exchanges and increase in market capitalization of 
the regional markets. In Oman, the listing of Omantel has significantly 
increased the liquidity and the market capitalization of the Muscat Securities 
Market. The total number of listed companies in the GCC has increased from 492 
at the end of 2004 to 579 in 2005, an increase of 17 per cent, which is a 
phenomenal growth. Another positive impact of the IPOs has been the improved 
performance of the secondary market, as it has significantly increased trading 
activity and increased the number of retail investors in the domestic market. 
The increased level of IPO activities has also helped the local banks as most of 
them were involved in financing subscriptions.<br>
<b><br>
Impact: </b>There is no doubt that in the long-term, IPOs give fillip to the 
performance of stock markets. However, as we have witnessed, a substantial 
portion of the money invested in the IPOs is generated from the equity markets. 
This behavior of investors tends to put irrational selling pressure on the 
market and results in increasing volatility. The huge numbers of IPOs to be 
offered in the regional stock markets may keep the GCC investors busy in their 
own markets. Thus, the fund flow from the regional investors may slow down, 
which will leave the MSM depending more on the local investors.
