<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<b>Oil Prices</b><br>
The continuous increase in international oil prices has helped the regional 
economies in their GDP growth. The Sultanate of Oman will also benefit from the 
current upsurge in international oil prices, as it would help the country 
achieve increased export revenues and high fiscal earning. However, the falling 
oil production, coupled with strong imports partly due to weak Dollar, will 
constraint the GDP growth.</font></p>
