<p align="left" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="5">Petrodollars &amp; GCC sovereign reserves</font></b><font face="Verdana" size="1"><b><br>
Matein Khalid talks about how petrodollars are stimulating an asset bubble in 
the GCC and peripheral</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="prescope.jpg" align="left" hspace="5" vspace="2">F</b></font><font face="Verdana" size="1">or 
the first time since the Organisation of Petroleum Exporting Countries (OPEC) 
oil shock of the 1970’s, Arab petrodollars reserves are once again a tidal wave 
in the international financial markets. The International Monetary Fund (IMF) 
estimates that annual current account surpluses could rise above US$300 billion 
as long as crude oil prices average US$60 a barrel, meaning that almost a 
trillion dollars of Arab petrodollars could well “seep” into the global markets.
<br>
<br>
Yet, unlike the situation in the 1970’s, petrodollars will not be passively 
recycled via international money centre banks into Latin America. Gulf 
petrodollars have stimulated an asset bubble in the GCC and peripheral. Arab 
stock markets and reinvented property finance in the Gulf Cooperation Council (GCC) 
have led to a proliferation of new funds, merchant banks, mergers and 
acquisitions in blue chip listed Western companies (as DP World proves, along 
with all their attendant political fallout). They are increasingly deployed in 
Asian emerging markets such as India, Pakistan, Malaysia and China. Yet, as the 
dramatic rise in Gulf central bank and sovereign investment agency purchases of 
the US Treasury bonds suggests, the vast bulk of newly minted petrodollars are 
invested into Uncle Sam’s IOU, as was the case after 1976, when the Saudi 
Arabian Monetary Agency rivaled the Bank of Japan as the largest offshore 
creditor of the United States government. <br>
<b><br>
Reserve Management Strategy</b><br>
It is impossible to understand the reserve management strategies of the GCC oil 
exporters without a sophisticated appreciation of the role of the Dollar in the 
international financial system and the geopolitics of GCC–American relations in 
the new millennium.<br>
<br>
Gulf sovereign investment agencies and central banks are extraordinarily 
overnight in dollars, reflecting the greenback’s own role as the world’s reserve 
currency and the paper money in which crude oil and high tech arms sales are 
denominated. However, both the Qatari and the UAE central banks have publicly 
indicated their intention to increase their reserves holdings in the Euro and 
the Japanese Yen. While such news occasionally triggers shock waves in the 
foreign exchange market, Arab petrodollars will reinforce, not threaten, the 
Dollar’s role in the international monetary regime, for the GCC states are all 
conservative, pro-American monarchies whose black gold sales are denominated in 
dollars and whose security umbrella is underwritten by the United States.<br>
<b><br>
Gold Bull Market</b><br>
However, the bull market in gold reflects Arab speculation as the geopolitical 
temperature rises in Iran, exactly as it happened in 1979, when the Shah lost 
his Peacock Throne and America lost its most loyal gendarme in the Middle East. 
It is alarming that despite a devaluation of the Dollar against the Euro after 
2002, the US trade deficit has ballooned now to US$700 billion, making American 
external finances akin to a Third World banana republic headed for the IMF/Paris 
Club’s intensive care unit. So the GCC’s loss of confidence in the Dollar could 
be fatal for the quasi-Bretton Woods systems of managed currencies and Federal 
Reserve dominance on which the international monetary architecture is designed. 
If, as Russia, Iran and Venezuela suggest, oil is denominated in dollars, the 
impact on the real value of Gulf sovereign reserves could be catastrophic. 
Ironically, Iran’s proposal to create a global crude marker after West Texas 
Intermediate and North Sea Brent makes perfect sense. The EU does import more 
Gulf oil than the US. If the Iranians discount their crude on the Persian NYMEX, 
then the impact on the Dollar could be catastrophic, leading to the biggest 
convulsions on the global currency casino since the Plaza/Louvre Accord of the 
1980’s. It may be recalled that in 1985, the United States mounted a calibrated 
Dollar depreciation operation with the concurrence of the G-7 countries for the 
good of all.<br>
<b><br>
Gold Versus Oil</b><br>
In any case, few international bankers appreciate the significance of the common 
GCC currency, a proxy Dollar or Euro, which could well be the unit in which Gulf 
oil is invoiced and the reserve currency of choice for the Arab — indeed the 
Islamic world. After all, if oil is the world’s mission critical commodity, gold 
is the only currency whose value is not subject to manipulation by a central 
bank.<br>
<br>
GCC sovereign investment agencies vary dramatically in scale, mandate and assets 
under management. The Abu Dhabi Investment Authority, for instance, is the model 
of professional, prudent management of the state’s oil reserves, untouched by 
the sort of scandal that once plagued Brunei and the Kuwait Investment Office in 
the 1990’s. It is one of the world’s largest institutional investors, even 
though its exact strategy and asset allocation is shrouded in secrecy. <br>
<br>
ADIA has global investments in private equity, hedge funds, commodities and 
emerging markets but it never seeks management control. Euromoney estimates that 
ADIA manages US$500 billion in global markets, making it a quasi — pension fund 
akin to BP, Calpers or IBM. It is interesting that Singapore, Norway, Dubai and 
Qatar have modeled their sovereign investment agencies after ADIA.<br>
<b><br>
High US Stakes</b><br>
The stakes for the US are incalculable. The Dollar accounts for 70 per cent of 
global foreign reserves, meaning the US finances US$2.5 billion a day of trade 
deficit from abroad. If there were a Euro-denominated oil commodity exchange in 
the Middle East, a GCC Dinar (gold Dinar?) and reserves diversification into 
gold accelerates, the Dollar is toast! <br>
<br>
Venezuela and Iran, and even Russia and China, have a political motive to use 
the Dollar’s role as the global reserve currency as an instrument of economic 
warfare against the US. It is not coincidental that China is nervous about its 
monster US$800 billion Dollar reserves placed entirely in Dollars. If GCC 
central banks and sovereign investment agencies follow China’s lead, expect 
fireworks on Wall Street leading to a high volatility scenario on all fronts 
which is not good.<br>
<br>
<i>Matein Khalid is a renowned investment banker based in Dubai. His regular 
monthly column will focus on the economic happenings in the Gulf Cooperation 
Council member countries and their impact on the global finance.</i></font></p>

