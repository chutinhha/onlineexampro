<b>A ‘long’ row</b></font><font face="Verdana" size="1"><br>
<b>A growing controversy over golf course changes has led to claims that the 
game now favours the few big-hitters<br>
</b><br>
</font><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="golf_update.jpg" width="190" height="145" align="right" hspace="5" vspace="2">M</b></font><font face="Verdana" size="1">edia 
reports, of late, have been abuzz with talk of new developments in the golfing 
arena. The controversy centres around the golf course! A row has raged on over 
changes in golf courses. There is a growing feeling among the “ golf biggies” 
that these changes are now favouring the big-hitters, much to the detriment of 
the old world charm of this majestic game. <br>
<br>
Interesting analogies are being drawn. Like, for example, the football analogy. 
What happens if the penalty spot on a football pitch is moved 10 yards further 
away from the net? Obviously, penalty takers, who seek to guide the ball into 
the goal, rather than bang it, will be on course to unemployment. <br>
<br>
Similarly figuring in the media reports is the tennis court analogy. What 
happens if the baseline of a tennis court is 5 feet further away from the net? 
Obviously, the game will belong, mainly, to “rocket launchers” such as Andy 
Roddick. <br>
<br>
<img border="0" src="golfupd1.jpg" width="200" height="287" align="left" hspace="5" vspace="2">The focus of the golf row has been, recently, the Augusta National Golf Club, 
where the guardians of the Masters, first of the season’s four majors, were 
accused of turning their course, a historic one, into a paradise for power 
hitters. <br>
<br>
There was a furore over confirmation that six new tees had seen Augusta National 
grow by 155 yards to a mammoth 7,445 yards, making it the second longest course 
in major championship history.<br>
<br>
Jack Nicklaus, winner of a record six Masters green jackets, was reported to be 
aghast. “I think they have ruined the tournament,” he said. Arnold Palmer, with 
four Masters titles to his credit, added: “I love the place but now I am not 
sure.” <br>
<br>
However, adding length to a golf course as a means of defence, against the new 
breed of big-hitting young players exploiting advances in club and ball design 
to the hilt, is not new.<br>
<br>
In case of Augusta National, however, argument raged over whether these latest 
changes detracted from the original vision of golf course architect Alister 
Mackenzie and Bobby Jones, co-founder of the Masters, which sought to place a 
premium on the skill of shooting to some of the game’s toughest greens.<br>
<br>
Hootie Johnson, chairman of Augusta National Golf Club, came to the defence of 
the new-look holes. He insisted: “As in the past, our objective is to maintain 
the integrity and shot values of the course as envisioned by Bobby Jones and 
Alister Mackenzie.”<br>
<br>
But Mike Weir, the 2003 Masters champion, believes they have got it wrong. The 
Canadian is among the shortest hitters on the on the USPGA Tour, standing 172nd 
in the average driving distance table on 275.3 yards, and after playing two 
practice rounds he said: “I am sure if Bobby Jones were around it would be like 
‘what are you guys doing?’ What disturbs me is they’re getting away from the 
character of the golf course. I think Mr Jones wanted creative shots from all 
over the place into tough greens. They’re taking away some of the excitement of 
the tournament.” Meanwhile, it is hoped the row will bring the game to a new 
equilibrium. After all, change is inevitable. Isn’t it?
