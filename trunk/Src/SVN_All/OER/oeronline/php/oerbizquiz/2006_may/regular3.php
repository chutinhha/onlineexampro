<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
As the Government continues to spend huge amounts on infrastructure and 
development, there is a huge demand potential emanating from the construction 
activity. The external economic environment also holds a lot of promise for the 
domestic economy as international crude oil prices touched a historic high of 
US$75 per barrel during this month. <br>
<br>
The Banking/Investment sector lagged behind by 3.37 per cent, pulled down by 
Oman &amp; Emirates and Oman International Bank. Despite the weak performance, the 
sector still holds a lot of potential for growth, since Banking and Investment 
plays a key role when an economy is on a sustained path of development.<br>
<br>
Another positive development that is sure to strengthen the banking sector is 
the Central Bank of Oman’s decision to reform the consumer loan ceiling for 
commercial banks. The consumer loan ceiling has been reduced from 42.5 per cent 
to 40 per cent and a new ceiling of 5 per cent for housing loans has been 
introduced.<br>
<br>
Therefore, the total personal portfolio has been increased to 45 per cent, 5 per 
cent housing loans and 40 per cent personal loans. Commercial banks are to 
comply with this decision by March. As real estate prices continue to increase 
and more developments under tourism and industrial are underway, there is 
potential for more demand in housing loans. Hence, this will be beneficial for 
the commercial banks, as we could be seeing a moderate growth in their loan 
books.
