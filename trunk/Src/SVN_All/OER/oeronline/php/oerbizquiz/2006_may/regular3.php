<b>Better days ahead</b></font><font face="Verdana" size="1"><br>
<b>Companies from the industry sector mainly dominated the major gainers in the 
Muscat Security Market<br>
</b><br>
The overall market remained lackluster during the month of April as the MSM 30 
Index closed marginally lower by 0.11 per cent to close at 5176 levels. The 
sector indices witnessed mixed trends, with Banking/Investment Index and 
Industry Index moving in the opposite direction. The Banking/Investment Index 
continued to nosedive further, by 3.4 per cent, to close at 7184 points. The 
Industry Index this month picked up remarkably well after the huge losses 
witnessed last month and jumped by 9.2 per cent. The Service Sector Index 
continued to drift slightly downward as the previous month, and ended in the 
red, down by 0.4 per cent.<br>
<br>
In terms of YTD growth, all the sectoral indices recorded healthy gains, 
reflecting the good company performance seen in the year 2005 and overall 
positive sentiments in the market. Moreover, we expect to see the correction to 
come to an end soon.<br>
<b><br>
Gainers &amp; Losers</b><br>
The major gainers in the Muscat Security Market were dominated mostly by 
companies from the industry sector. Among them were Al Hassan Engineering (47 
per cent), followed by Majan Glass (21 per cent) and Raysut Cement (19. per 
cent). From the service sector, Al Maha Petroleum touched a historic high of RO 
8.2 during this period and recorded a gain of 18 per cent.</font></p>
