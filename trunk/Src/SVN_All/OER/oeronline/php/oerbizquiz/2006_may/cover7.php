            <img border="0" src="omanair.jpg" width="290" height="79" align="left"><br>
            <br>
            <br>
            <br>
            EYEING THE ‘niche’ tag</b></font><font face="Verdana" size="1"><b><br>
            Though Oman Aviation has slipped by two ranks over last year, the 
            company continues on the profitability track despite record increase 
            in fuel costs</b></font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="omanair1.jpg" width="200" height="275" align="left" hspace="5" vspace="2">F</b></font><font face="Verdana" size="1">ollowing 
            the financial turnaround in 2004, Oman Air has once again reported a 
            profit in 2005. Net profit for 2005 is RO 1.006 million, compared to 
            RO 0.628 million in 2004. Revenue increased by RO 10.62 million over 
            the last year from RO 65.72 million to RO 76.34 million. The 
            performance is especially commendable as the aviation industry 
            suffered from significantly higher fuel costs. <br>
            <br>
            Fuel cost rise continued unabated throughout the year. The average 
            fuel price in 2005 was 36 per cent higher compared to the previous 
            year. “This increase meant an increase of RO 3.934 million in fuel 
            bills for 2005,” says Said Bin Hamdoon Bin Said Al Harthy, Chairman, 
            Oman Aviation Services. <br>
            <br>
            The operating parameters also showed further improvement during the 
            year. Oman Air carried 1,134,658 passengers, up 15 per cent over 
            last year. The average seat factor across the network rose from 71 
            per cent to 76 per cent. The aircraft utilisation was high at 12.5 
            block hours per day per aircraft. In the airport services business, 
            Oman Air handled 257,000 more passengers, 4,864 tonnes of additional 
            cargo, and catered 531,000 additional meals to airlines. There was 
            noticeable improvement in all major business activities. <br>
            <br>
            “The expenditure increased only by 15 per cent — RO 9.387 million to 
            RO 74.022 million — as against a 16 per cent increase in revenue. 
            The increase in cost is mainly on account of fuel, which rose by RO 
            4.778 million or 46 per cent. This was mainly due to a 36 per cent 
            increase in fuel price and partly due to increase in operations 
            compared to the previous year. <br>
            <br>
            While most costs increased commensurate with increase in operations, 
            catering material cost increased only by 11 per cent, as against a 
            19 per cent increase in revenue. It is important to note that but 
            for the increase in fuel price, operating profit would have been 
            even better compared to previous year,” explains Ziad Karim Al 
            Haremi, Acting CEO of Oman Aviation Services.<br>
            <br>
            During the year, Oman Air added one B737-800 aircraft, commenced two 
            new routes, and upgraded all the regional routes to jet operations. 
            Two surplus ATR42-500 aircraft were leased to an airline in India.<br>
            <b><br>
            Human Resources</b><br>
            Oman Air recruited 10 Omanis for pilot training during 2005. They 
            will be trained over 18 months, after which they would qualify as 
            First Officers. This initiative reflects the company’s relentless 
            drive to promote Omanisation, one of the key national objectives.
            <br>
            <br>
            Further, the company recruited eight Omani graduates as management 
            trainees in 2005. This follows the first intake of 10 Omani 
            graduates in 2003. The key objective of the management training is 
            to develop these Omanis as future managers who will assume key 
            positions in the company. At the end of 2005, the company has 
            attained 75 per cent Omanisation, which is a remarkable feat 
            considering the nature of airline business, which requires 
            multi-national and multi-lingual staff to serve its customers. <br>
            <b><br>
            Future Challenges</b><br>
            2005 witnessed greater passenger and cargo volumes and higher 
            capacity utilisation. However, higher fuel prices during the year 
            meant the industry as a whole was still in the red in 2005. “With 
            the region’s economies expanding, on the back of rising oil prices, 
            Middle East carriers have outperformed the industry average with 
            higher growth in passenger and freight traffic. This trend, along 
            with the buoyant economic growth in Indian sub-continent, a major 
            market segment for Oman Air, should enable us to consolidate our 
            presence as a niche regional carrier,” says the Chairman. <br>
            <br>
            The Chairman further says: “Fuel prices have remained high during 
            the first quarter of 2006 and continue to pose a serious challenge 
            to Oman Air as it does for the rest of the airline industry. In 
            airport services business, the company is gearing up to meet the 
            challenges of increased flights, passenger volumes and freight 
            traffic. Much of this increase will result from the shift of some of 
            Gulf Air’s operations from Abu Dhabi to Muscat. <br>
            <br>
            “The Government of Oman has also embarked upon an ambitious project 
            to modernise and expand the airports at Seeb and Salalah. The 
            company is working in close co-ordination with the Government, 
            DGCAM, Oman Airports Management Company (OAMC) to ensure its support 
            and contribution to the success of this project.” <br>
            <br>
            Ziad adds, “Although, the region has witnessed a build up of vast 
            capacity and resultant competition from some large carriers, we 
            believe that the strong focus on customer care and innovative 
            product development can lead to an increased market share, greater 
            passenger traffic and revenue growth for the company. <br>
            <br>
            “Sustained monitoring of costs, combined with economies of scale 
            through measured expansion on profitable regional routes, can 
            improve margins in the company’s airline business.”</font></p>
