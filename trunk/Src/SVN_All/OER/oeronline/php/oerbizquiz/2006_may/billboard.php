                            resulted in the establishment of Oman’s first LNG 
                            plant, ‘Oman LNG’ in 1994. In 2002, the Government 
                            of Oman decided to move forward with plans to 
                            develop a third gas liquefaction train and thus 
                            ‘Qalhat LNG’ was born and incorporated in 2003. </font>
