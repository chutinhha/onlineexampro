            CEMENT COMPANY</a></b></font><font face="Verdana" size="1"><b><br>
            Registers record sales and profits since its inception </b><br>
            <br>
            Oman Cement Company SAOG (OCC) achieved sales of 1.820 million MTS 
            of cement during 2005 as against the sales of 1.646 million MTS of 
            cement during the corresponding period in 2004. In value terms, the 
            sales for 2005 were worth RO 43.981 million as against the sales of 
            RO 33.878 million in 2004. <br>
            <br>
            OCC moves up two positions to secure the 11th position in the OER 
            Top 20. The improvement in average selling price of cement by 17 per 
            cent and increase in sales volume by 10.5 per cent led to the 
            increase in sales value. As a result, OCC recorded RO 18.147 million 
            as the profit after tax in 2005 against RO 13.172 million in 2004.<br>
            <br>
            The demand for cement has been very high in the local market and the 
            company is operating at more than full capacity. The company has 
            been successful in obtaining better price realizations because of 
            higher prices prevailing in neighboring region. In order to meet 
            increased local demand, it had to import clinker as well as bulk 
            cement at high prices. <br>
            <br>
            The demand projection for 2006 is expected to be similar to 2005. 
            However, there could be enhanced local supply, which could put 
            pressure on the company. OCC has invested RO 725,000 in its 
            subsidiary, Sohar Praton and Concrete Products SAOC (SPCP), with a 
            view to reviving the company.<br>
            <br>
            OCC was established in 1978 as 100 per cent owned by the Government 
            of Oman. By 2003, the Government successfully managed to bring down 
            its shareholding in the company to 51 per cent. The percentage of 
            Omanisation in the company is 57 per cent.</font></p>
