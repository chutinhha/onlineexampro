            </b><br>
            The company recorded a net profit of RO 10.24 million in 2005 as 
            compared to RO 10.21 million in 2004, which is 28 per cent higher 
            than 2004, excluding the extraordinary income of RO 2.2 million on 
            account of the settlement of liquidated damages. The turnover of the 
            company indicated a 2 per cent revenue growth from RO 42.5 million 
            in 2004 to RO 43.5 million in 2005. The company makes an entry in 
            OER Top 20 ranking this year.<br>
            <br>
            AES Barka SAOG’s principal activities are developing, financing, 
            designing, constructing, operating, maintaining, insuring and owning 
            a power generating station and water desalination plant and 
            associated gas interconnection facilities and other relevant 
            infrastructure. <br>
            <br>
            The increase in revenue owes to a combination of better availability 
            as well as better load factor for 2005 in comparison to 2004. The 
            net profit margin in 2004 remained the same, at 24 per cent, where 
            the general and administrative expenses increased by 11 per cent. 
            The company needs to keep a check on its operating expenses. The EPS 
            for the year 2005 was at RO 0.320. It paid a dividend of 24 per 
            cent, amounting to RO 7.680 million in the form of two interim 
            dividends during the year 2005. In comparison, the company paid a 
            dividend of 47 per cent in 2004.<br>
            <br>
            The share underwent a long consolidation in a 10 per cent price band 
            with an average of RO 2 in the entire year of 2005. It took off in a 
            major way on 27 December 2005 from a price of RO 2.10 and went up 
            very steeply after that to cross the RO 3.25 mark. </font></p>
