<p align="justify" dir="ltr"><font face="Verdana" size="1">Gulf Air has added 69 
more flights from its Muscat hub as part of its summer schedule. Shane O’Hare, 
Head of Marketing, Gulf Air says, “This makes a total of 195 flights per week 
flying out of Muscat from the earlier 126 flights per week. Together with our 
code share partner Oman Air, we now offer more than 330 flights from Muscat per 
week. So Gulf Air takes the travelers to GCC destinations, the Middle East, Far 
East, the Indian subcontinent, Africa and Europe, with a wide range of options.”<br>
<br>
In terms of traffic, Gulf Air flew 1.6 million passengers through Seeb Airport 
in 2005. And this figure is expected to grow to 2.6 million in 2006, accounting 
for 55 per cent of the Seeb Airport activity. <br>
<br>
But from where does the additional traffic to Muscat is expected to come? O’Hare 
responds: “With a wider connectivity, we are expecting a huge spike in traffic 
volume — both local as well as transit. In terms of value, our focus is on 
premium segment, comprising of first class and business class segments, which 
contributes more than 80 per cent of our profits.” <br>
<br>
To strengthen itself as the national carrier of Oman, Gulf Air has embarked on a 
major marketing exercise in the Sultanate. It was the gold sponsor of the Muscat 
Festival 2006. It is sponsoring a number of other local programmes to touch the 
lives of Omanis. For the first time, Gulf Air organised its top management 
conference in Oman recently. The airline is expected to announce a major 
initiative in May to strengthen its bonding with Oman.<br>
<b><br>
Stumbling Blocks</b><br>
Despite scoring highly on the management front, Hogan and his 5,700 plus strong 
team, drawn from 62 different nationalities, face numerous challenges. Like 
Qatar and Abu Dhabi, Oman also has a separate national carrier — Oman Air. What 
is the guarantee that Oman will not pull out from the airline like the other 
two? An industry analyst says, “From the commercial angle, it doesn’t make sense 
for Oman to keep investing in two different airlines simultaneously. Either 
there has to be a clear demarcation in their product offerings or they should 
take the merger route to align their strengths.”<br>
<br>
Hogan refuses to be drawn into this controversial, but important question. He 
comments: “It is up to the shareholders to decide on the future of Gulf Air and 
Oman Air. I cannot comment on this. As of now, we see clear synergy between the 
two airlines. We don’t compete with each other. Rather, we complement each other 
quite well.” <br>
<br>
With other airlines from the region strongly supported by their much stronger 
economies, facing the (unfair) competition will be a tough task for Gulf Air. 
Unlike Gulf Air, a number of other airlines can afford to sink billions of 
dollars in network expansion and aggressive marketing, thanks to fuel hedging 
and support of their respective governments. <br>
<br>
Though Oman holds a great potential as a tourism destination, its infrastructure 
— including airport and hotels — will take a few more years to expand 
substantially. However, Hogan is unfazed. “We have done it before. We are much 
better equipped to meet the challenge than ever before. We will further build on 
our reputation as the leading regional carrier.” </font></p>
