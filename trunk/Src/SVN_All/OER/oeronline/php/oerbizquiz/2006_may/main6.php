<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Focus on Oman</b><br>
With the withdrawal of Abu Dhabi, Oman now controls 50 per cent of Gulf Air’s 
shareholding. As a result, the airline is focusing on its Oman operations in a 
major way. Gulf Air is already running its call center, with a seating capacity 
of 300 to cater to the global requirements. It is located at Knowledge Oasis 
Muscat (KOM). The airline has also signed an MoU to make Swiss-based SR Technics 
Holding assume total responsibility for the maintenance, repair and operation of 
the airline’s fleet in a five-year contract valued at US$750 million. It 
includes the establishment of an aircraft maintenance facility and training 
centre based in Oman. The Oman based US$50 million three-bay hangar and 
maintenance facility in which Gulf Air will have a 10 per cent shareholding, 
will provide maintenance solutions based on customer requirements, include 
aircraft and component services, technical training, logistics, on-wing and 
light engine services and engineering for Gulf Air and other third party 
customers. <br>
<br>
Talking about the benefits Gulf Air will bring on the table for Oman, HE Shaikh 
Mohammed Al Harthy, the Minister of Transport of Oman, says: “The cumulative 
benefits to the local economy are significant with suggested estimates of at 
least one per cent to the GDP of the Sultanate. When fully operational, the 
joint venture will create jobs for 850 skilled and professional people over the 
initial five-year period. In addition to the enormous employment opportunities, 
the venture presents for Omanis, a subsidiary maintenance training center, which 
will also offer world-class technical training for airline maintenance engineers 
and technicians for the regional industry. It will also create an environment in 
which downstream businesses and industries can develop and flourish, generating 
further revenue and employment opportunities.” </font></p>
