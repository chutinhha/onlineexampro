<br>
</font><font face="Verdana" size="5">TWO STEPS FORWARD, ONE STEP BACKWARD</font></b><font face="Verdana" size="1"><b><br>
Gulf Air, the 56-year old airline brand, is making a concerted effort, 
to re-emerge as a commercially sustainable regional carrier after a series of 
setbacks. Akshay Bhatnagar, in a special report following a recent visit to 
Bahrain, uncovers Gulf Air’s new growth strategy and its possible impact on 
Oman’s economy</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="gair1.jpg" width="250" height="130" align="right" hspace="5" vspace="2">D</b></font><font face="Verdana" size="1">espite 
a complete change in airline business dynamics, Gulf Air enjoys a very high 
brand awareness and recall among air travelers and trade circles the world over, 
especially in the Middle East, the Indian subcontinent, South East Asia and West 
Europe. It has faced numerous challenges in its journey but it has always 
managed to bounce back, only to retract again. With another round of change in 
ownership and a new three-year growth strategy in place, the US$1.3 billion 
carrier is striving to build on its recent gains in an age which its President &amp; 
Chief Executive James Hogan calls “times of unfair competition”.<br>
<br>
“Today we are ranked among the world’s top ten airlines in products and 
services. We have been making record gains in revenues and traffic and winning 
industry accolades. Despite that, in 2005, we were back among losses due to very 
high fuel costs. In 2002, fuel accounted for 14 per cent of our cost. Today, it 
is a staggering 31 per cent of our cost,” informs Hogan. <br>
<br>
An industry analyst adds, “Gulf Air is perhaps the best managed airline in the 
region but the high fuel bill will continue to affect its bottom line. A number 
of other leading airlines in the region have the advantage of “fuel hedging” 
which means they are either not required to pay for their fuel bills or they get 
fuel at highly subsidized rates from their respective governments. Fuel hedging 
enables them to invest heavily in building capacity and introducing price cuts 
to drive other carriers out of business.”<br>
<b><br>
</b> </font><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="sprep.jpg" width="200" height="108" align="left" hspace="5" vspace="2"></b></font><font face="Verdana" size="1"><b>Smart Airline, Successful Business </b><br>
As a sequel to Project Falcon (see box ‘Chequered History’ for more on Project 
Falcon), Gulf Air has adopted a new three-year strategic plan “Smart Airline, 
Successful Business”. It entails significant investment for the 
re-capitalization of the airline, re-equipment of the fleet, product upgrades 
and refurbishment of present aircraft, and investment in a range of areas of the 
business.<br>
<br>
Gulf Air, in the advent of Abu Dhabi’s withdrawal, has initiated a two-hub 
policy focusing on Bahrain and Muscat. “The new two-hub strategy will result in 
significant cost reductions and bring about major improvements to our key 
operational indicators, including punctuality, as we will have greater fleet 
flexibility and an even more effective set of connections,” says Hogan.<br>
<br>
On being queried whether he is under pressure to make more use of Muscat as the 
other important hub, Hogan replies, “All our decisions are based on sound 
commercial basis. In my view, Oman is the most exciting tourism destination in 
the entire region. The increased influx of tourists and expansion in hotel 
capacities is a testimony to the tourism potential of Oman.”<br>
<br>
A consortium of lenders, including Commercial Bank of Qatar, First Commercial 
Bank — Singapore, National Bank of Oman, State Bank of India — Bahrain, Persia 
International Bank, and The Arab Investment Company, will extend credit 
facilities valued at US$75 million, repayable over a period of three years, to 
Gulf Air. <br>
<br>
The airline has invested in the US$10 million state-of-the-art simulator center 
at Bahrain recently. The airline has also signed an agreement for the provision 
and installation of new First and Business Class seats in its Airbus A340 
aircraft and the simultaneous refurbishment of the aircraft interiors to match 
the standards of the award-winning A330 fleet. Gulf Air will also install new 
in-flight entertainment systems during the year. In addition, the airline is 
expected to invest at least US$1 billion in acquiring new wide body aircraft 
from either Boeing or Airbus. A decision to this effect is expected by May or 
June this year.</font></p>
