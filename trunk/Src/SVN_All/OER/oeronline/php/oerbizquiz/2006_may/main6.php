    <p align="center" style="margin-top: 10px; margin-bottom: 10px">
    <font face="Verdana" size="1"><b>CHEQUERED HISTORY</b></font></p>
    <p align="justify" style="margin: 10px"><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="gair2.jpg" width="150" height="116" align="right" hspace="5" vspace="2"></b></font><font face="Verdana" size="1">The 
    Gulf Aviation Company, the forerunner of Gulf Air, was born in 1950 with the 
    suppor</font><font face="Verdana" size="1">t of the ruler of Bahrain. It was the brainchild of former Royal Air 
    Force (UK) pilot Freddie Bossworth. British Overseas Aircraft Corporation 
    (BOAC) took over the operations management of the company in 1951. For the 
    next two decades, the company continued to grow its network by acquiring new 
    aircraft and extending flights to new destinations to connect the Gulf 
    region with the rest of the world. <br>
    <br>
    In a related development, the British pulled out of the region and the 
    former British Protectorate States of Bahrain and Qatar, and the Trucial 
    States (now part of the United Arab Emirates) declared Independence. 
    Consequently, the governments of Bahrain, Oman, Qatar and Abu Dhabi took 
    over the ownership of the company from BOAC. The company was renamed ‘Gulf 
    Air’, the national carrier of the four states in 1974. At the same time, 
    OPEC (Organization of the Petroleum Exporting Countries) began raising the 
    oil prices, which led to a major economic boom in the Gulf region.<br>
    <br>
    If the ’70s was the great boom time for the airline, the 80s provided a 
    bumpier ride. It suffered losses in 1986 and 1987. The recession in the 90s, 
    which had gripped the international airline industry, had a serious effect 
    on Gulf Air too, despite the airline’s long-term plan to avoid it. 
    Profitability fell and debt kept on mounting. In 1996, a two-year target was 
    worked out to return the airline to profitability. Tough measures were 
    implemented, including the closure of low-yield routes, and disposal of 
    aircraft and some subsidiary shareholdings. The airline returned to 
    operating profit in 1998, aided by a massive reduction in debt. <br>
    <br>
    Gulf Air celebrated its 50th anniversary in 2000. But even before the 
    celebrations could die down, troubles were back. To arrest the downslide and 
    change the fortune of Gulf Air, James Hogan was appointed as the new 
    President and Chief Executive of the airline. <br>
    <b><br>
    Signs of turnaround</b><br>
    Despite his good track record, Hogan, the tough Aussie, had an enormous task 
    on his hand. Gulf Air was pushed to rank 72 among the list of top airlines 
    in the world. To make matters worse, Qatar, predictably, pulled out of the 
    ownership to focus on its own national carrier, Qatar Airways. Gulf Air 
    ended 2002 with losses of US$110 million. <br>
    <br>
    Hogan introduced the three-year restructuring and turnaround programme named 
    ‘Project Falcon’. More than US$238 million was injected into the airline by 
    the three owner states in 2003 and government debt continued to be deferred. 
    “We focused on the pillars of the recovery plan, including brand 
    development, fleet re-construction, network, alliances and customer service 
    etc. There was a strong focus on profitability and improvements to cost 
    structures,” Hogan informs. <br>
    <br>
    In 2003, a new corporate identity, devised in conjunction with leading brand 
    consultancy Landor, was unveiled as part of the progressive change 
    management programme. “The re-styled falcon, which remains the focal point 
    of our new corporate identity, communicates a synthesis of bold, modern 
    entrepreneurial principles and traditional Arabian values, in what we 
    believe is a powerful prominent icon for a new era,” says Hogan.<br>
    <br>
    The rebranding was one of the several initiatives which helped Gulf Air to 
    get back in black again after a gap of six years, recording a profit of US$4 
    million in 2004 against a loss of US$52.8 million in 2003. But just when it 
    seemed the airline was back on track to regain its lost glory, disaster 
    struck again. In 2005, Abu Dhabi gave up the ownership to concentrate on its 
    own national carrier, Etihad Airways. <br>
    <br>
    External factors have continued to make the operating environment more 
    turbulent for Gulf Air. Developments such as the ongoing conflict in Iraq, 
    weak US dollar, spiraling fuel cost, and increased competition from new 
    low-cost airlines and existing regional and global carriers are casting a 
    dark shadow on the financial performance of the airline. However, as the 
    saying goes, “where there is a will, there is way.” And going by its track 
    record, it will be no surprise if Gulf Air manages yet another turnaround.
