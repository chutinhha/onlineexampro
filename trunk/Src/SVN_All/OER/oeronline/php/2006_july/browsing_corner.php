            </font><font face="Verdana" size="4">
            The king who corrected mess</font></b><font face="Verdana" size="1"><br>
            <br>
            Mehre Alam reviews the book ‘Kingdomality: An Ingenious New Way To 
            Triumph in Management’<br>
            <br>
            </font><font face="Verdana" size="2"><b>
            <img border="0" src="browsing_corner1.jpg" align="left" width="150" height="204">O</b></font><font face="Verdana" size="1">nce 
            upon a time — in the medieval times to be more precise! — there 
            lived a ruler called King Harold. His empire had all the bounties. 
            From overflowing wealth to iconic army, his Kingdom had it all. If 
            it lacked in something, it was a proper system. There was chaos all 
            around. Unfortunate as it was — projects got started there but 
            rarely finished. Meetings called to discuss how to get a project 
            concluded would soon turn into meetings discussing why the project 
            was not completed; this in turn meant everybody disowned any 
            responsibility for the mess and blamed everyone else.<br>
            <br>
            Thus begins Kingdomality, a book authored by Sheldon Bowles, Richard 
            Silvano and Susan Silvano, all management gurus in their own right. 
            Needless to say, King Harold is the corporate czar of the 21st 
            Century, and his kingdom, a corporate. Through the idiom of this 
            medieval kingdom, the authors hammer the point straight — why a 
            corporate clicks, and why it fails, on the issues of human 
            resources.<br>
            <br>
            The moral of the story so far: Here is a Kingdom with poor 
            productivity and unhappy, unreliable, and unfulfilled subjects 
            banding together with friends and turning on, rather than working 
            with, each other. Its projects have five characteristics — 
            enthusiasm, trouble, search for a scapegoat, punishment of the 
            innocent, and finally, praise and reward for all non-participants! 
            In short, a situation similar to many modern-day organizations... 
            may be your very own! Sounds familiar?<br>
            <br>
            Now, the king had tried everything to set things right — from 
            flogging to distributing lollies. But nothing worked. Tired of not 
            getting results, he turns to veteran management consultant Lady 
            Elizabeth, who guides him through the concept of Kingdomality. 
            Following King Harold, the reader meets 12 medieval characters, each 
            having likes and dislikes; environments where they flourish and 
            where they falter. By the end of the journey, King Harold knows 
            exactly how to assign tasks to each of his subjects and transform 
            his floundering kingdom to a flourishing country. <br>
            <br>
            Kingdomality pinpoints 12 types (see box) of employees — and 
            recognizing their strengths and weaknesses — and claims to offer 
            practical business improvement tools to help managers to flourish in 
            kingdoms large and small. Using the medieval kingdom as a parable 
            for a business environment, Kingdomality provides an engaging, and 
            interesting tool for analysing and improving the workplace.<br>
            <br>
            Now, coming to the secret. KING. That’s the word. It has the 
            answers, the key to rejuvenate the kingdom, nay the corporate! How? 
            Sample this: K stands for “know yourself, know others”. I stands for 
            “Identify the issues”. N stands for “name your team”. And finally, G 
            stands for “Get goals and get going”. <br>
            <br>
            The authors do come up with an interesting parable. But it all 
            depends on how you look at it. In the age of gurus, and obviously, 
            management gurus, it’s almost sin to act as an agnostic. Just 
            remember the last time you were hauled up by your boss for failing 
            to lend a focused ear to the virtues of his guru. Needless to say 
            the guru had changed by the time the next meeting happened. And this 
            time, the paeans were loftier, and the disdainful look (for turning 
            a blind eye), even more piercing.<br>
            <br>
            Try telling him/her this: Unlike the natural sciences, the 
            behavioural sciences lack accuracy of lab-conditions, so they are 
            obviously, much, much less accurate. And that this science of gurus 
            does not even fall in the domain of behavioural sciences! That this 
            is no rocket science but plain common sense. One shudders, though, 
            to ponder whether, like in the case of King Harold, is common sense 
            that rare a tool so that one has to look towards the gurus to imbibe 
            all that? For, didn’t the king need to be told that different human 
            beings have different strengths, and weaknesses too! And that he 
            needed to tap the plusses the right way. That finding scapegoats 
            will serve an organisation no good. That in being biased towards one 
            employee, and promoting the other unabashedly, one only sends the 
            wrong message. Hope one doesn’t sound too way off the mark. Truth to 
            be told, Kindomality does deserve a read. At least for managers!<br>
            <br>
            About the authors: The book is written by Sheldon Bowles, Richard 
            Silvano and Susan Silvano. Bowles has co-authored, along with Ken 
            Blanchard, many successful books including Raving Fans and Gung Ho! 
            He is president of a management company, and founding president of 
            Domo Gasoline Corporation, which he built into a US$75 million 
            company. He lectures throughout North America and Europe and lives 
            in Canada.<br>
            <br>
            Richard Silvano and Susan Silvano have built their Houston-based 
            company Career Management International, Inc. into a 
            multi-million-dollar company. They have worked for many years in 
            career counselling, employee training and development with 
            individuals, as well as helping companies, government agencies and 
            educational institutions throughout the world to realise the full 
            potential of their most precious investment, their human resources.<br>
            <br>
