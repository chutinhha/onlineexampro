<p align="justify" style="margin-right: 10px"><b>
<font face="Verdana" color="#FF0000" style="font-size: 14pt">
<br>
</font></b><font face="Verdana" size="6"><b>Sponsor me!<br>
</b></font><font face="Verdana" size="1"><b><br>
Event marketing is the latest buzz with the Middle East markets, writes Matt 
Harrison</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1"> recent 
survey revealed that event marketing has become a top choice for marketers — 44 
per cent of those polled chose event marketing over advertising at 18 per cent, 
direct marketing at 15 per cent while public relations, sales promotion and 
Internet advertising registered just single-digits.<br>
<b><br>
Event Marketing Booms</b><br>
Globally, trade shows were perceived by one-third of those surveyed as the 
leading event type in terms of Return of Investment (ROI). Following behind 
trade shows in perceived ROI were conferences (24 per cent) and seminars (20 per 
cent). Across all geographies, event marketing averaged 20 per cent of total 
marketing communications budgets. This trend is most visible in Asia Pacific, 
where 74 per cent of the survey’s participants cite the growing influence of 
event marketing, almost 20 points higher than the global average. In fact, more 
Asian Pacific corporations (49 per cent) anticipate increased budget allocations 
to event marketing than for any other marketing activity. <br>
<br>
I fully expect Middle East markets to follow this marketing trend. Eng. Mohammed 
Al Ghassani, Executive Vice President of the Public Establishment for Industrial 
Estates — responsible for Oman’s six industrial estates as well as Knowledge 
Oasis Muscat (KOM) — said: “Whether it’s increased service knowledge, greater 
brand awareness, a boost in space rentals or a reinvigorated work force, 
face-to-face events are instrumental in helping PEIE and KOM advance their 
strategic goals.” In short, event marketing is big and getting bigger.<br>
<b><br>
Wealthy Greeks</b><br>
Events whether they’re seminars, workshops, conferences or football tournaments 
can provide golden opportunities not just for those participating, but also for 
companies aiming to promote their brands to a large audience. For those involved 
in organising events, whose aim is often to maximise the generation of income, 
corporate sponsorship is one of the most lucrative sources of revenue. <br>
<br>
If you look back at history, the origins of sponsorship can be found in Ancient 
Greece, where wealthy Athenians contributed hard earned cash to cultural, 
defence and sports events. This was to help events become more accessible to 
Citizen Stavros. In return, sponsors were honoured by having their names 
engraved on marble tablets. This tribute <br>
was a mark of the city’s respect, value and appreciation.<br>
<br>
In more recent times, corporate sponsorship has been viewed mainly as a 
commercial transaction where a sponsor pays a given fee and in return receives 
certain rights. In most cases, in addition to the fee, the sponsor also supplies 
the sponsored party with their products or services. In return, corporate 
sponsors expect to receive exclusivity in the use of the official logo, be given 
unique advertising and promotional opportunities, on site concessions/franchise 
and product sales together with the right to describe themselves as the official 
sponsors of the event in their marketing and promotional campaigns. <br>
<br>
It is estimated that brand marketers worldwide will spend US$50 billion on 
sponsorships in 2006. Indeed, sponsorship and event marketing opportunities have 
proliferated as interest in opportunities and spending has increased. <br>
Oman-based firms have a domestic event buffet to choose from, we’ve rallying, 
camel racing, golf and football tournaments through to musical concerts, 
exhibitions, eGames conferences, monthly Open House seminar programs to mobile 
marketing tours, sampling and product placements on TV. <br>
<br>
<b>The Feel Good Factor</b><br>
From a local perspective, let’s say you’ve just sponsored a local business 
conference or a sporting event to the tune of RO3,000 — what impact will it have 
on the local community? — what kind of buzz will it generate? Here are two 
examples, the first is from a person living in Los Angeles who attended a track 
and field event during the 1984 Olympic Games:<br>
<br>
“This is really fantastic! The crowds ... the excitement. I’m so glad I came. I 
thought I wouldn’t attend any [Olympic] events. I don’t like sport. I was even 
sorry that the Olympics were going to be in LA. But there was so much energy ... 
people were so excited. I just had to be part of it. So here I am. It really 
doesn’t matter what is happening down there (on the track). There is just so 
much energy! People are so happy! It’s just fun!”<br>
<br>
The second story is from a young Sydney Olympic Volunteer who describes an 
incident she was involved in: “My (volunteer) assignment finished late. I was 
still in my (volunteer) uniform, and I had to change trains at Cabramatta. I 
wouldn’t have been there if I didn’t have to be. It isn’t safe. I was alone on 
the platform and (a teenager) walked up. He looked tough, and I thought he was 
going to rob me. He said, “I want to thank you for what you are doing for our 
country.” Then he just walked away.”<br>
<br>
There are hundreds of stories like these at most events whether they’re business 
or sport-oriented. There is a sense that something important is happening — a 
sense that is felt more than understood. If the occasion is a sporting event, 
the sport may be the catalyst, vehicle, or rationale for the felt sense of 
importance, but is neither the object nor the cause. <br>
<br>
The sporting outcomes may matter to some, but there is a sense of something more 
important happening. In brief, something that transcends the sport that is going 
on. It feels as if new energy has been injected into the community — an energy 
that can be shared by all. There is a heightened sense of community among those 
who attend the event. If you’re looking for an incentive to sponsor an event and 
get your brand associated with a good-feel vibe, then this is it. <br>
<b><br>
Return on Investment </b><br>
Naturally, bigger Rial allocations means more intense scrutiny from senior 
management. In turn, the demand for accountability and demonstrable ROI from 
sponsorships and event marketing has never been higher. <br>
Unfortunately, tools for assessing the performance of sponsorships and event 
marketing in terms of contribution to sales and profitability have always been 
in short supply. According to research, 60 percent of marketers say they are not 
satisfied with their ROI tools. All I can say on this point is if you don’t 
measure it, then it’s obvious you don’t care about it!<br>
<b><br>
Big Business Idea Competition</b><br>
However, the goal of sponsoring an event, let’s say a business plan competition, 
has to be about making the brand message become part of the event because, if 
done successfully, the two will be inextricably linked and always associated 
with one another. <br>
<br>
For example, the recent involvement of Ernst &amp; Young with the launch of the 
Knowledge Mine — Ernst &amp; Young Big Business Idea Competition (www.kom.om/bbic) 
will without doubt build brand equity and brand awareness for Ernst &amp; Young. 
Just tagging a name onto an event and not supporting the sponsorship fully leads 
to a disassociation between the name of the event and the actual brand. <br>
<br>
How often have you seen corporates give over cash to an event but not get behind 
the event? Indeed, just attaching your name to an event is lazy marketing. It is 
not about the corporate name; it is about what will happen pre- during and 
post-event: how the brand will come to life and how the target audience can 
experience the brand image at the event. The corporate needs to be aware of this 
and act on it.<br>
<b><br>
Work Together</b><br>
A successful event sponsorship deal is very similar to a joint venture or 
partnership in that the parties work closely together and assist each other to 
achieve their respective commercial aims. Don’t underestimate this commitment. 
Sponsorships tend to be long-term relationships, many lasting for two, three or 
four years. <br>
<br>
As the activities of parties to a sponsorship contract can affect the reputation 
and goodwill of each other, it is important that both parties make enquiries of 
the other prior to contract as part of the commercial evaluation of a 
sponsorship proposal. If this is done properly then real and meaningful ROI will 
be had. <br>
<i><br>
Matt Harrison is a Muscat based marketing consultant.</i></font></p>

