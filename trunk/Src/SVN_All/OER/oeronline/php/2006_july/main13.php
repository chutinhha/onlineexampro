<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><font face="Verdana" size="5">
<b>A 4-wheel for first-timers!<br>
</b></font><font face="Verdana" size="1"><br>
<b>The all-new Daihatsu Terios SUV comes as more stylish, bigger and powerful 
compared to its previous avatar, OER finds out in the test drive </b><br>
<br>
</font>
<b><font face="Verdana" style="font-size: 11pt">
<img border="0" src="cartalk1.jpg" align="right" hspace="5" vspace="2">T</font></b><font face="Verdana" size="1">hose with a nose for sporty, 
stylish, compact 4-wheel drives, who lack the will to bear the price, can look 
forward to the all-new Daihatsu. The new model is quite different from its 
predecessor. The new compact SUV is far superior to the old one in terms of 
size, space, power, style, features, and overall performance. <br>
<br>
As I took the vehicle for a drive, I was impressed by its easy maneuverability 
during rush hour, and its handling on the rugged terrain near Azaiba.<br>
<b><br>
Performance and Safety</b><br>
The newly developed 1.5-litre engine generates enough power to make driving a 
pleasant experience, even with the compact SUV’s expanded body. The new engine 
delivers ample torque at low to mid-rpms and smooth, easy acceleration at high 
rpms. The engine promises to be more fuel-efficient too as the company claims a 
low fuel consumption of 12.65 km per litre on an average. As the traffic in Oman 
is quite smooth, the low fuel consumption might appeal to many, especially those 
having to burn long hours in heavy traffic in other parts of the Gulf region. It 
comes in both 5-speed manual as well as 4-speed automatic transmission.<br>
<br>
Terios offers full-time 4WD to quickly accelerate from a standing position and 
outstanding high-speed stability. The center differential prevents occurrence of 
the “tight corner-braking phenomenon.” In thick sand or on a significantly bumpy 
road, just touch a button to switch on the differential lock. This connects the 
front and rear wheels directly, giving the vehicle better traction. <br>
<br>
The 15-inch disc brake system assures satisfactory stopping performance. The EBD 
(Electronic Brake force Distribution) and ABS (Anti-lock Braking System), with 
brake, assist distributing the braking force optimally to the four wheels. The 
newly developed suspension system (Front — MacPherson Strut and Rear — 5-Link 
Coil Spring) gives the Terios stability even when the road gets rough. This new 
system will help minimise fatigue during long-distance travel and improve both 
riding comfort and driving stability. <br>
<br>
Though it may appear much smaller than its bigger 4-wheel counterparts, its 
short front and rear overhangs and the ample clearance gives the Terios large 
approach and departure angles. This reduces the possibility of damaging the 
bumpers when driving over high obstacles such as large stones or while climbing 
a rocky hill. With its small turning circle, Terios navigates through narrow 
roads with ease and squeezes into tight parallel parking spaces comfortably. <br>
<b><br>
Interior &amp; Comfort </b><br>
The interior of the new Terios is quite impressive, with its sporty and modern 
design. The thick instrument panel has a neat straightforward design. Metallic 
parts adorn the technical-looking centre cluster and other interior parts. The 
cabin interior expresses uniformity, a high-quality feel and individuality. <br>
<br>
One also enjoys ample space in the driver’s seat. A 700-mm distance between the 
driver’s seat and front passenger seat ensures plenty of room for two adults to 
sit in comfort. The driver seat hip point is 740mm above the ground, raising the 
driver’s eye point to a height that assures better forward visibility for the 
driver, thanks to a large vertical viewing angle. <br>
<br>
The driver’s seat is designed in a manner that fits drivers of different heights 
and physiques. A seat height adjuster raises the driver’s seat or lowers it 
substantially. The Terios also features tilt steering. The adjustable seatbelt 
anchors slide up and down to accommodate people of various sizes. Together, 
these features assure that drivers of almost any size and physique find just the 
right driving position.<br>
<br>
For those looking for more luggage space - for heavy shopping or outdoor trips - 
it is the right car. It offers decent luggage space although it may not appear 
so from the outside. The Terios can accomodate five adults on board. The seats 
can be configured in several ways, depending on the cargo-carrying needs. Fold 
down one seatback of the 60-to-40 split rear seat, and Terios can carry long 
items. Or one can fold and stow the rear seat to create ample flat-floor luggage 
compartment.<br>
<br>
For entertainment, it comes with AM/FM Tuner with ETR and 4-speaker CD stereo 
system. In the SPL model, the multi-information display shows clock, average 
fuel consumption, traveling range, outside temperature, and altitude. <br>
<br>
The new Terios is positioned as a ‘fun wheel drive’. It is targeted at auto 
lovers — especially first-time SUV users with a penchant for a sporty and 
pleasurable drive. The compact Japanese SUV, is attractively priced at RO5,400 
onwards,and is expected to find more than enough takers in the Sultanate. <br>
<br>
Road test courtesy Saud Bahwan Automotive LLC (Daihatsu Division), the dealers 
of Daihatsu vehicles in Oman.</font></p>
