<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="4">‘Oman is a bipolar market’</font><font face="Verdana" size="6"><br>
</font></b><font face="Verdana" size="1"><b><br>
Akshay Bhatnagar catches up with Ki Wan Kim, President, Middle East &amp; Africa, LG 
Electronics Inc., who was on his maiden visit to Oman</b><br>
<br>
</font><font face="Verdana" size="2"><b>
<img border="1" src="passingby_1.jpg" align="left" width="150" height="133" hspace="5">K</b></font><font face="Verdana" size="1">i 
Wan Kim, who moved in as the new President based out of Dubai for LG Electronics 
in the Middle East (ME) &amp; Africa, was completely surprised by the Sultanate of 
Oman on his first trip here after assuming the office in February this year. “To 
be honest, Oman is much better than I had anticipated. Muscat is very clean and 
beautiful. I’ve not seen such clean roads anywhere in the ME. And Omanis are one 
of the most friendly people in the Arab world,” he said. <br>
<br>
What brought him to Oman? “I’m here to participate in the ongoing celebrations 
to commemorate LG’s ten successful years in the Sultanate. Through our local 
partners OTE Group, I have also launched the new sleek and trendy LG Chocolate 
mobile phone in Oman,” he informed. <br>
<br>
Even though he’s fairly new in his position, he is familiar with the ME and 
Africa as he was handling the global operations for certain product categories 
within LG fold earlier. He realises the importance of the region in LG’s quest 
to become one among the top three electronics, information and 
telecommunications firm in the world by 2010. Sharing LG’s performance and 
vision for the region, Kim said, “With windfall oil revenues, many countries in 
the region are developing their infrastructure rapidly and the lifestyle of the 
people is also improving fast. This trend has opened a big market for us in the 
corporate and consumer segments alike.”<br>
<br>
He added further, “The ME &amp; Africa currently contributes 7 per cent of LG’s 
global revenues (US$23,217 million in 2005). For the last decade, our operations 
are growing by almost 15-20 per cent on an annual basis. But last year our 
growth rate dropped to single digit. The main challenge came on the front of the 
mobile business, as it didn’t grow as per our expectations.” <br>
<br>
<b>Vision 2010 on Track</b><br>
With a negative growth rate in global sales in 2005, LG faced a setback towards 
achieving its Vision 2010. Kim accepted the fact that 2005 was a bad year for LG 
and 2006 is also going to be difficult and challenging but claimed that the 
company is on track to achieve its Vision 2010. He said, “In terms of 
geographical markets, our financial performance was affected mainly due to 
adverse market conditions in markets including Russia and China. Product wise, 
we moved well in all product segments except the mobile business. We are the 
number one player in CDMA based mobile phone category globally but we need to 
catch up in the GSM category which comprises the major chunk of the mobile 
market. We are the inventor of 3G but it is an expensive technology for the 
consumers and consumed mainly in the developed markets like Europe. ”<br>
<br>
He added, “Our resources and priorities are focused towards growing our mobile 
business. As part of this exercise, I’m here in Oman to launch and promote the 
new LG chocolate mobile phone. It is an important step in the direction to 
transform and place LG’s mobile brand in the same league as the market leader 
Nokia in the consumer mindset. Every month from now on we are going to launch a 
new mobile product in the market to maintain the momentum.”<br>
<br>
On the change in the company’s management strategy, he said: “LG has shifted its 
strategy and policy by implementing the ‘Blue Ocean Management,’ which we 
believe will ensure our resounding success. Blue Ocean Management is a creative 
new style of management which opens new markets and invents new methods and 
systems that will maximize management efficiency. In other words, this is a 
management strategy for a whole new paradigm in which we will compete in the 
areas that we have to, while developing a blue ocean area which does not have 
any competition thus creating more profit. To this end, this year, we will focus 
on customer oriented management, strong technological competitiveness, and 
operational excellence.”<br>
<br>
Networking and engineering are the buzzwords at LG’s research &amp; development and 
design labs. Kim informed, “From being just a device supplier, we are 
positioning ourselves as a total networking solutions provider. For example, we 
provide total electronics and information products networking solutions for an 
intelligent home. This kind of specialized solutions is in great demand among 
the design consultants and architects. The regional market here has a good 
potential for such solutions which are currently more in demand in highly 
developed markets.”<br>
<br>
He also emphasized on the growing trend for personalized and individualistic 
products. He elaborated: “Ten years back, people used to like listening to music 
with their friends and families. But today people like to listen to music on 
their own personalized MP3 players. Individualized products are in demand now. 
To develop new products to meet consumer’s penchant for personal consumption 
gadgets is a focus area for us.” <br>
<br>
<b>Regional Market</b><br>
<br>
On the consumer characteristics of the regional market, Kim said, “The markets 
here are bipolar. Take Oman for example. There is demand for high end of the 
spectrum products and simultaneously, there is a constant requirement for low 
end products too. Take TV segment for example. On one hand, there is demand for 
big size Plasma TV and on the other hand, people are looking at 20 inch 
conventional CRT based TV sets too. A company needs to meet the demand of both 
the segments.” <br>
<br>
Despite claiming that the region is a focus area for growth, not many 
electronics and information products manufacturing companies are coming forward 
to set up their R&amp;D centres in the region to develop new technologies. Kim 
agreed and clarified on the reasons behind this: “It is difficult to find good 
local talent in most parts of the region to establish and manage a world class 
R&amp;D centre here. That’s why we look at countries like India where there is large 
pool of skilled workforce. In this age of cutthroat global competition, we have 
to innovate at a very fast pace efficiently. The states in this region have to 
benchmark themselves with countries like India and China and take steps 
accordingly to encourage research and design development activities which could 
prepare and produce new technologies and products which are not just useful in 
this region but could be used globally.” <br>
<br>
On LG’s performance in Oman, Kim said, “LG has been doing very well with OTE 
Group as its partners in progress. They have a sound long term vision which is 
in alignment with LG’s business growth strategy.”<br>
