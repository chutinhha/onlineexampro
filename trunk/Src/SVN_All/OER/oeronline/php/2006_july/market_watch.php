<b>‘Faconnable’ watches launched in Oman<br>
</b><br>
<img border="0" src="marketwatch_1.jpg" align="left" hspace="5" width="150" height="190"></font><font face="Verdana" size="2"><b>J</b></font><font face="Verdana" size="1">K Enterprises LLC, has 
unveiled the latest line from Swiss watch brand ‘Faconnable’. Available at 
Alasfoor Plaza in Qurum, the new range includes two lines for men and five lines 
for ladies watches. These are exclusive jewellery design watches which are 
highly successful in the global market. Among the men’s range, “Time Off Gent” 
line is a fine mix between design and watch-making know-how. Under this range, 
the technical elements of the movement are brought onto the watch case and dial. 
The axial jewels hold the dial platform and the bouchonne finishing decorates 
the case cover. It comes in three dial colours — silver, black and Champaign. 
The other line “Hydra Titanium” has a titanium bracelet in deep grey colour. The 
remodeled dial comes with engraved slight waves, a Facconable symbol. It comes 
in two versions — “3 Worlds Titanium” with rugged style along with elegance and 
targeted at travelers as it offers three movements for three different time 
zones and “Hydra Gent Titanium” with three hands movement. Prices of the new 
range start from RO400 upwards. All watches come with at least one 
interchangeable strap and a multi-purpose storage case.</font></p>
