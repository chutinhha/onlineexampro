<p align="justify" style="margin-right: 10px"><font face="Verdana" size="1"><b>
LG – flat panel TV<br>
</b><br>
<img border="1" src="marketwatch_2.jpg" align="right" hspace="5" width="150" height="135">To encash the World Cup 
Football fever, Oman Gulf Enterprises has launched the region’s first (as per 
company’s claim) flat panel TV with a built-in DVR made by LG. The 42-inch TV’s 
built-in 80GB DVR can store upto 30-hours of digital standard-definition 
programming, equivalent to nearly 20 World Cup football matches. It can pause 
and replay just minutes after the original broadcast, and block out unwanted 
commercials with a touch of a button. The LG DVR PDP also boasts of continuous 
automatic recording in one-hour intervals. LG’s exclusive XDTM Engine promise to 
improve brightness, contrast, and 3D colour control. It offers sharpness 
enhancement and 3D noise reduction, and the XDS and SRS/BBE Sound System 
delivers cinematic surround sound. In Oman, OTE Group, is the exclusive 
distributor of LG .The new LG DVR PDP is available through LG showrooms as well 
as authorized dealer outlets in the Sultanate of Oman.</font></p>

