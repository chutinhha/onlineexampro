                <p align="justify"><font face="Verdana" size="1">
                <b>Arcelor accepts Mittal takeover bid<br>
                </b><br>
                <img border="1" src="worldscan_arcelor.jpg" align="left" width="150" height="114">Arcelor 
                SA ended a bitter five-month takeover battle by yielding to a 
                sweetened bid from Mittal Steel Co. The combination will create 
                a titan with nearly 10 per cent of the global steelmaking 
                market. Arcelor’s board supported the proposal to create 
                Arcelor-Mittal after Mittal boosted its offer 7 per cent to 
                about US$33 billion. Since Mittal launched a surprise bid in 
                January this year, Arcelor had wrung several changes out of the 
                company, retaining a considerable level of control. <br>
                <br>
                Analysts have said the two companies are complementary and have 
                few overlaps. Arcelor — based in Europe with large operations in 
                Latin America — focuses on high-quality steel, selling to 
                long-term clients in the auto industry in Europe. 
                Rotterdam-based Mittal’s steel mills in Asia and Eastern Europe 
                produce lower-quality steel sold on the open market, although it 
                also has significant operations in North America.<br>
                <br>
                Under the terms of the deal, Mittal will pay US$50.51 a share in 
                cash and stock, a 49 per cent improvement on Mittal’s first 
                unsolicited offer on January 27. The Mittal family, which 
                currently holds 87 per cent of Mittal Steel, will own 43.6 per 
                cent of the combined group and agreed not to make any major 
                changes to their stake for five years. The new company would 
                dwarf other steel makers, controlling close to 10 per cent of 
                world steel production, churning out 120 million tons of crude 
                steel a year and employing more than 320,000 people. It would 
                have a market capitalization of US$46 billion and save US$1.6 
                billion in synergies.</font></p>
