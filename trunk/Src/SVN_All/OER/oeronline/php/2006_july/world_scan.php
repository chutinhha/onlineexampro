                <p align="justify"><font face="Verdana" size="1">
                <b>Nestle fattening up on skinnier foods<br>
                </b><br>
                The world’s biggest food company well known for its chocolate 
                and coffee has embarked on a new push to tackle obesity and 
                diabetes. At the company’s research laboratory in Switzerland, 
                scientists are working on new projects that alter the body’s 
                absorption of sugar, reduce fatty acids in the bloodstream and 
                set up the burning of carbohydrates during digestion. The first 
                of these offering a cereal bar with a fiber addictive that tamps 
                down surge in blood sugar levels after eating, was launched in 
                Asia last year and will soon be rolled out worldwide. More 
                engineered foods are on the horizon. Nestle is studying 
                acetogenic fibers, found in apples and some vegetables that may 
                reduce high level of fatty acids in the bloodstream.</font></p>
                <hr color="#000000" width="80%" size="1">
                <p align="justify"><font face="Verdana" size="1">
                <b>Future without Gates</b><br>
                <br>
                Microsoft is mapping plans for a future without visionary Bill 
                Gates as the world’s biggest software giant gears for battle 
                with Google and others to be the dominant high-technology 
                company. Bill Gates, who founded Microsoft with his childhood 
                friend Paul Allen, has announced he will step down from his 
                day-to-day duties at the company in 2008 so that he can devote 
                more time to philanthropy. Gates will retain the title of 
                chairman of the group he founded over 30 years ago, while 
                handing over his title of “chief software architect” to Ray 
                Ozzie, another software industry pioneer.<br>
                <br>
                According to analyst group Gartner, Microsoft is facing one of 
                its greatest challenges yet. “Recent trends such as Web 2.0, 
                software as a service and open source software are increasingly 
                threatening the company’s pre-eminence within the IT industry. 
                Google represents the most obvious challenge.”<br>
                <br>
                The response to this challenge will be led by chief technology 
                officer Craig Mundie, who will take on Gates’ research and 
                strategy responsibilities, and Ray Ozzie, who will become chief 
                software architect. <br>
                <br>
                Commenting on the change, George Kalorkoti, group chief 
                information officer at solicitors Herbert Smith, said, 
                “Microsoft is at the stage in its evolution where a fresh set of 
                directions, in response to the business challenges it faces from 
                the likes of Google, Linux and the EU (competition issues), is 
                going to benefit it and its customers.”</font></p>
