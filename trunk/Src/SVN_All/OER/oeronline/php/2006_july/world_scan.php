                <br>
                Nokia and Siemens to join hands<br>
                <br>
                </b></font>
                <b>
                <font face="Verdana" size="2">
                T</font></b><font face="Verdana" size="1">wo of Europe’s biggest telecommunications companies, Nokia 
                and Siemens, have announced their intention to join forces. The 
                new company, named Nokia Siemens Networks, will have about 
                US$19.9 billion in annual sales and will be based in Finland. 
                Nokia is located at Espoo, Finland, while Siemens is based in 
                Munich. The combined entity will have around a 21 per cent share 
                in the wireless-equipment market.<br>
                <br>
                The companies say that they will eliminate around 9,000 jobs — 
                15 per cent of their combined workforce — with the aim of 
                mounting a challenge against market leader Ericsson AB, which 
                has a 26 per cent share of the market. Siemens had been 
                searching for years for a solution for its telecom equipment 
                unit “Com”, after offloading its loss-making mobile phones 
                division to Taiwan’s BenQ last year. Nokia and Siemens said they 
                both expect to boost their earnings per share by the end of 2007 
                on a pro-forma basis excluding restructuring charges.<br>
                <br>
                The European Commission is generally supportive of the agreement 
                between the two companies. In an interview, the EU’s 
                telecommunications commissioner Viviane Reding said the merger 
                would be viewed as an opportunity to create a stronger player 
                and enhance competition: “When there are mergers that help the 
                market to be competitive, which is a very good move.”</font></p>
