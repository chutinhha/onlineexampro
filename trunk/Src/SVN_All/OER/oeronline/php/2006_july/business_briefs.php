                        <b>eOman awareness programme at Ibri<br>
                        </b><br>
                        eOman Road Show, the Sultanate’s e-Government awareness 
                        programme from the Information Technology Authority (ITA), 
                        was held between 17-21 June at Ibri in the Adh-Dhahirah 
                        region. The ITA Marketing &amp; Awareness Team conducted the 
