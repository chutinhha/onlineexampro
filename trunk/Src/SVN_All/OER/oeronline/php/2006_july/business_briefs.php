                        <font face="Verdana" size="1"><b>InfoMart workshop draws 
                        interest</b><br>
                        <br>
                        InfoMart/Moody’s, in association with the Capital Market 
                        Authority, organised a workshop on “Asset-Backed Debt 
                        Markets” in Muscat on June 11. Conducted by Khalid 
                        Howladar, Vice-President for Moody’s Investors Service, 
                        securitisation was the main theme of the workshop. The 
