                        <font face="Verdana" size="1"><b>CBO eases norms on 
                        banks’ minimum capital adequacy <br>
                        </b><br>
                        Central Bank of Oman (CBO) has eased its minimum capital 
                        adequacy norms for banks by reducing the minimum capital 
                        adequacy ratio to 10 per cent from 12 per cent. CBO is 
                        implementing the new international capital accord ‘Basel 
                        II’ from early next year. To implement Basel II in 
                        totality, banks had started new capital accord 
                        calculation from January this year. The new 10 per cent 
                        ratio will be applicable after the implementation of 
                        Basel-II. In 1998, CBO had implemented minimum capital 
                        adequacy ratio of 12 per cent for banks.<br>
                        <br>
                        CBO has also raised the minimum capital requirements for 
                        new and existing leasing and finance companies from RO5 
                        million to RO10 million. A timeframe of three years is 
                        in place for leasing and finance companies to comply 
                        with the minimum capital requirements of RO10 million.</font></p>
