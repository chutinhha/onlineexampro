                        <font face="Verdana" size="1"><b>TKM–Ernst &amp; Young Big 
                        Business Idea Competition<br>
                        </b><br>
                        The 2006 TKM–Ernst &amp; Young Big Business Idea Competition 
                        was launched recently. In its first year, the 
                        competition is seen by many as a launch pad for 
                        participating entrants to successfully raise investment 
                        for their commercial start-up ventures. It is organised 
                        by The Knowledge Mine Incubator Program, Ernst &amp; Young 
                        and the Information Technology Authority. The organisers 
                        particularly encourage Omani researchers, students and 
                        entrepreneurs to enter, but also welcome submissions 
                        from individuals and teams. The ideas should be original 
                        or a significant improvement on an existing business. 
                        This year the competition has a top prize of RO6,000 for 
                        the winning business plan.<br>
                        <br>
                        Those who submit a business plan will get high-level 
                        feedback on their idea and the chance to learn more 
                        about processes of business. “The competition is a 
                        highly-prestigious platform for successful new venture 
                        creation, access to networks, training and valuable 
                        contacts, and it helps further understanding of the 
                        investment process and what constitutes a realistic 
                        marketable idea,” said Sultan Al Habsi, CEO, Public 
                        Establishment for Industrial Estates. <br>
                        <br>
                        The last date for submission of the business plan is 
                        September 2. The semi-finalists will be announced in 
                        mid-September, with the final itself taking place at the 
                        Muscat Inter-Continental Hotel on November 5. “As Oman‘s 
                        economy continues to grow, Ernst &amp; Young is pleased to 
                        play a role in furthering business development in the 
                        Sultanate,” said Philip Stanton, Managing Partner. “We 
                        will also be running pre-competition workshops at 
                        Knowledge Oasis Muscat, June 25, July 2 &amp; 9, and these 
                        sessions are open to all,” commented Stanton. The full 
                        details on the Big Business Idea Competition and along 
                        with an application form can be downloaded from
                        <a href="http://www.kom.om/bbic">www.kom.om/bbic</a></font></p>
