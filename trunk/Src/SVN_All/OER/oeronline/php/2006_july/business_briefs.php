                        <font face="Verdana" size="1"><b>IRF workshop on 
                        economic liberalization<br>
                        </b><br>
                        The International Research Foundation, in coordination 
                        with the Ministry of Commerce &amp; Industry, OCIPED, MENA-OECD 
                        Investment Programme, and Center for International 
                        Private Enterprise (CIPE) and The Fraser Institute of 
                        Canada, organised a workshop at the Shangri-La’s Barr Al 
