                        <b><br>
                        Oman’s trade surplus crosses RO1.1b in Q1<br>
                        <br>
                        </b>
                        <img border="1" src="biz_briefs1.jpg" width="175" height="86" align="left" hspace="5"></font><font face="Verdana" size="2"><b>O</b></font><font face="Verdana" size="1">man’s 
                        trade surplus mounted to an all-time high of RO1.1 
                        billion in Q1 2006 from RO686.3 million in Q1 2005, 
                        according to provisional figures released by the 
                        government. This record increase is attributed mainly to 
                        high oil prices. The exports soared 35.3 per cent to 
                        more than RO2.05 billion in Q1 2006. Oil and gas exports 
                        increased by 38.5 per cent in Q1 2006 to over RO1.75 
                        billion. The crude oil exports were to the tune of 
                        RO1.43 billion, recording an increase of about 36 per 
                        cent over the corresponding period of the preceding 
                        year. Non-oil exports increased by 7.4 per cent to 
                        nearly RO142 million. Re-exports rose by more than 32 
                        per cent to RO162 million.<br>
                        <br>
                        Oman had recorded a trade surplus of more than RO3.7 
                        billion in 2005 compared to RO1.748 billion in 2004. 
                        Trade balance for the years 2001, 2002, 2003 and 2004 
                        stood at RO1.976 billion, RO1.874 billion , RO1.871 
                        billion and RO1.748 billion, respectively. The aggregate 
                        GDP was provisionally estimated at over RO15 billion for 
                        the year 2005.</font></p>
