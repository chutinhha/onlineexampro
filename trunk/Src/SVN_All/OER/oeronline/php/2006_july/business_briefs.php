                        <font face="Verdana" size="1"><b>Oman to reduce debt by 
                        30 per cent<br>
                        </b><br>
                        The Omani economy will take advantage of high oil prices 
                        to reduce its debt by up to 30 per cent this year. Oman 
                        was using windfall revenue to reduce its debt, which 
                        amounted to US$2.6 billion. Oman’s internal debt stood 
                        at US$1.6 billion and its external debt was US$1.03 
                        billion. The 2006 state budget carries a gap of US$1.69 
                        billion but it is based on a conservative oil price of 
                        US$32 per barrel, about half the actual price. According 
                        to official statistics, Oman’s oil and gas exports 
                        soared 44.5 per cent to US$15.7 billion in 2005 over the 
                        previous year. The hydrocarbon sector accounts for 80 
                        per cent of the Sultanate’s export earnings and 40 per 
                        cent of its gross domestic product.</font></p>
