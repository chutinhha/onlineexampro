                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Alstom gives power contract to OHI subsidiary<br>
                        </b><br>
                        Douglas OHI, a subsidiary of Oman Holdings International 
                        (OHI), has won a contract worth RO12.5 million from 
                        France-based company Alstom. The French company is the 
                        EPC contractor for the 1000MW power plant, part of the 
                        US$2.4 billion Sohar smelter project. The mega power 
                        plant, one of the biggest of its kind in the region, 
                        will be the main civil engineering and building works 
                        contractor for the power plant. <br>
                        <br>
                        Douglas OHI LLC had won a contract worth RO11.9 million 
                        from Oman International Container Terminal to construct 
                        container terminal phase-A and phase-B at the Sohar 
                        Industrial Port. Douglas OHI is an Oman-UK joint 
                        venture. </font></p>
