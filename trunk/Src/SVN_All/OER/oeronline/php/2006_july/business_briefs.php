                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>BankMuscat signs Tejari<br>
                        </b><br>
                        BankMuscat took a step forward on to the B2B bandwagon 
                        as it entered into an agreement with Tejari-Omania 
                        E-Commerce LLC to move its procurement processes online 
                        via Tejari Transact. The agreement was signed between 
                        Abdul-Razak Ali Issa, chief executive of BankMuscat and 
                        Mohammed Al Harthy, managing director of Tejari-Omania 
                        E-Commerce LLC. By becoming a member of Tejari Transact, 
                        BankMuscat will be able to make all major purchasing and 
                        procurement online. The contract envisages that by the 
                        year-end all procurement and purchases, such as office 
                        supplies and equipment, made by BankMuscat will be 
                        through Tejari’s B2B e-marketplace.</font></p>
