                        <font face="Verdana" size="1"><b>Renaissance Services 
                        subsidiary Adyard wins Gasco contract<br>
                        <br>
                        <img border="1" src="biz_briefs_renaissance.jpg" align="right" width="150" height="97" hspace="5"></b>Renaissance 
                        Services subsidiary Adyard Abu Dhabi LLC (Adyard) has 
                        won a contract to supply equipment for the Habshan gas 
                        complex expansion project of Abu Dhabi Gas Industries 
                        Ltd (Gasco). The contract, worth US$3.6 million and 
                        granted by Fluor Mideast Ltd, entails the design, 
                        engineering and supply of 45 ASME coded CS pressure 
                        vessels. The work will be carried out at Adyard’s 80,000 
                        sq m waterfront facility located in the industrial city 
                        of Mussafah, near Abu Dhabi. The entire project is 
                        expected to take just over a year to complete and 
                        initial preparations are already in place. <br>
                        <br>
                        Adyard is one of the UAE’s leading state-of-art oil and 
                        gas services companies, specializing in engineering, 
                        fabrication and onshore and offshore projects. Adyard is 
                        part of the Topaz Group acquired by Renaissance last 
                        year.<br>
                        <br>
                        Renaissance Services SAOG is one of the largest public 
                        listed services companies in the region. A large 
                        proportion of Renaissance business is engaged in 
                        providing quality services to the oil &amp; gas sector, 
                        where it is a leading turnkey contract services 
                        provider; and has one of the top 10 marine support 
                        fleets in the world with a significant presence in the 
                        Gulf, the Caspian region and S.E. Asia. Renaissance is 
                        recognized as an Omani company with substantial 
                        international credentials, successfully exporting its 
                        services abroad.<br>
                        <br>
                        The company’s 2005 Annual Report states that 75 per cent 
                        of the annual revenue of RO106.4 million was generated 
                        outside Oman and 60 per cent of revenue is generated in 
                        services to the oil &amp; gas industry.</font></p>
