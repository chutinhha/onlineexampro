                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Oman set for cyber law<br>
                        </b><br>
                        Oman will soon have a cyber law. It is expected to come 
                        into effect by this year. “The cyber law is actually 
                        under revision by the authorities concerned and we don’t 
                        expect it to take more than this year,” said Dr Salim 
                        bin Sultan Al Ruzaiqi, head of the recently established 
                        Information Technology Authority (ITA). He further 
                        informed: “The e-legislation or the cyber law is already 
                        drafted and it is ready with the working group (drawn 
                        from about six to seven agencies, including ministries, 
                        the Central Bank of Oman, Omantel and the 
                        Telecommunications Regulatory Authority). The cyber law 
                        will cover all aspects of e-transactions, and e-commerce 
                        and would include private security or encryption, 
                        digital signatures and all issues related to the e-law.</font></p>
