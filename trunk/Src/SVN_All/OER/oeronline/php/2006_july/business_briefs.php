                        <font face="Verdana" size="1"><b>Omantel inks pact on 
                        e-Government network</b><br>
                        <br>
                        Omantel has inked an agreement to design, implement and 
                        manage a unified e-Government network with the Ministry 
                        of National Economy. The agreement is part of the 
                        government’s Digital Oman and e-governance initiatives. 
                        The agreement will lead to the creation of the first 
                        comprehensive network of its kind to bring all 
                        government bodies under a digital network. The agreement 
                        was signed by Mohammad Bin Nasser Al Khusaibi, 
                        Secretary-General of the Ministry of National Economy 
                        and Chairman of Information Technology Authority, and 
                        Saud Bin Nasser Al Shikaili, Chairman of Omantel. <br>
                        <br>
                        As part of the agreement, Omantel will build and operate 
                        a Convergent Government Network to connect governmental 
                        sites across the Sultanate. A pilot project of the 
                        Convergent Government Network was conducted prior to 
                        signing the agreement. The pilot project connected about 
                        30 sites across four ministries. It is estimated that 
                        over 415 sites of different ministries will be connected 
                        to the Convergent Government Network over the next four 
                        years.</font></p>
