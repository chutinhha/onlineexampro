<p align="justify" dir="ltr"><font face="Verdana" size="5"><b><br>
CAVE WONDER</b></font><font face="Verdana" size="1"><img border="1" src="spl_feature1.jpg" align="right" width="250" height="188" hspace="5" vspace="2"></font><font face="Verdana" size="5"><b><br>
</b></font><font face="Verdana" size="1"><b><br>
TOURISTS WILL SOON BE ABLE TO SIT IN A TRAIN AND ENTER THE AL HOTA CAVE, ONE OF 
THE MOST MAJESTIC CAVERNS IN OMAN, AND A TREASURE TROVE OF ANCIENT AND SPLENDID 
ROCK FORMATIONS. MEHRE ALAM REPORTS</b><br>
<br>
Imagine being stationed deep inside a cavern with a main chamber as big as the 
ballroom of Muscat’s Al Bustan Palace Hotel. And, all of a sudden, a lion roars 
in the pitch of darkness. Afraid? Need not! Rock-shaped speakers placed near the 
lion stalagmite in the cave are camouflaged with the surroundings, and emanating 
a roaring sound, when approached. Welcome to Al Hota Cave. A geological wonder 
that Oman’s Ministry of Tourism will soon showcase to tourists and scientists.<br>
<br>
Across the world, cave tourism is a major tourist attraction. Oman, which is 
home to some of the most awesome caves, is all geared up to give a fillip to 
cave tourism. Oman Economic Review learns, from sources, that Al Hota is the 
first of the three caves to be opened to cave tourism. Already riding high on 
increased tourist interest in Oman, Al Hota project comes with some breathtaking 
attractions. <br>
<br>
To begin with, this natural cave system is estimated to be 2 million years old. 
The rocks date back to the cretaceous era — they are between 65 million and 136 
million years old. The age of the magnificent stalactites and stalagmites — a 
profusion of splendid cave formations — in the cavern is estimated to 100, 000 
years. As a rule of thumb, one cm of stalactite takes 1,000 years to form.<br>
<br>
<img border="1" src="spl_feature2.jpg" align="left" width="166" height="161" hspace="5" vspace="2">The development of Al Hota Cave, a RO4 million venture of the Ministry of 
Tourism, is easily one of the most ambitious tourism projects of its kind in the 
region. The project is set to herald quite a few ‘firsts’ for Oman, including a 
railway track, a train, a tunnel and a cave station project. If sources are to 
be believed, the Al Hota Cave may be opened to the public sometime later this 
year. <br>
<br>
Situated in the foothills of the Central Al Hajar mountains, with exotic 
limestone hills in the background near the Wilayat of Al Hamra in the Dakhiliyah 
region of Oman, Al Hota is the second largest cave in Oman. The picturesque 
mountains amid which this cave is located fall a few kilometres from the city of 
Nizwa.<br>
<br>
The Al Hota project consisted of four packages — a visitor centre building 
housing a museum and a screening hall, a railway station, a bow string bridge, 
tunnel, cave station and development of caves, railway track and train, and 
specialised sound and lighting systems. And the news is that it’s almost 
complete. The railway tracks are already in place. “We are waiting for the train 
to arrive from Austria,” says M.V. Satish, Chief Manager, Larsen &amp; Toubro (Oman) 
LLC. (Out of the four civil packages for the cave project, three were awarded to 
Larsen &amp; Toubro (Oman) LLC) The concept unfolds like this: At the entrance of 
the premises that leads to the cave is a visitor centre building and a railway 
station. Tourists can relax, dine and browse the museum out here. From here, the 
tourists board a train and move off, on a narrow one-line track, through a 
tunnel. They disembark at a station. It’s quite like arriving at a railway 
station. <br>
<b><br>
Major Attractions</b><br>
The major attraction for tourists is, obviously, the natural cave, which is 
packed with magnificent stalactites and stalagmites — geological wonders, and a 
5-km long lake that boasts a species of blind fish and other elements of 
biodiversity. In fact, the pathway erected in the cavern ends at the foot of the 
lake, which flows to the village called Al Hota from which the cave derives its 
name. <br>
<br>
<img border="1" src="spl_feature3.jpg" align="right" width="286" height="171" hspace="5" vspace="2">The cavern is home to a subterranean lake connected to a chain of water bodies 
inside the cave, one of which links to the Tanuf water system of old times. <br>
<br>
“The geological goldmine that lies inside the cave is bound to attract a large 
number of academics, besides tourists,” says Anjan Mohapatro, Construction 
Manager, Larsen &amp; Toubro (Oman) LLC, and Project Manager for Al Hota Cave 
Project.<br>
<br>
What a student-tourist can hope to gaze into is a treasure trove of mankind’s 
geological past. It’s a majestic mingling of the 21st Century with the Jurassic 
Age. History unfolds with each step you take on the raised platform-pathway 
erected inside. The cave is punctuated with dozens of captivating ancillary 
chambers and offshoots. Gaze into the krastic cavity (punctures on the roof) and 
the artistry of nature is bound to leave you mesmerized. <br>
<br>
The formation of the Al Hota Cave started with the production of dilute acids 
formed by the rainwater dissolving carbon dioxide (Co2) from the atmosphere and 
soil. This acid dissolved the limestone from the top to bottom. <br>
<br>
Interestingly, the actual entrance to the natural cave system used to be through 
a sink hole of 20 metres height. It is this sink hole that was used in the past 
by people to enter and hide within as legend would have it. The second entrance 
has been made through an access tunnel of 148-m length. “The tunnel is reverse 
‘S’ shape in plan to avoid fault zones and fractured rock mass,” informs 
Mohapatro. <br>
<br>
“Communication systems are installed inside the cave for contact outside in case 
of emergency and the entire cave is monitored at the Visitor Centre Building 
through five cameras installed inside the cave,” Mohapatro adds.<br>
Crystal lights, fixed inside the cave at places, simulate stars in the sky. A 
light and sound system in the hall area enhances the natural beauty of the 
speleothems (stalactites and stalagmites). Soft music plays the moment one&nbsp; 
enters the hall area showcasing the huge speleothems. “But everything has been 
done in a way that it doesn’t disturb th<img border="1" src="spl_feature4.jpg" align="left" width="219" height="193" hspace="5" vspace="2">e bio-diversity at all,” informs 
Mohapatro.<br>
<br>
Is the cave going to be open to all? Or will there be some restrictions on the 
basis of health or age factors? As a general rule, people having heart ailments 
and breathing problems, or very old people, should avoid going deep inside the 
cave, say experts. <br>
<b><br>
</b><b>Development of the Cave Project</b><br>
The development of the cave comprises of the construction of 856 metres of 
pathways, and guard rails along the cave, bridging deep gorges, pits and big 
boulders without disturbing the inside environment, and installing sound and 
light systems all along the way.<br>
<br>
The guard way consists of 10 platforms at strategic locations, structural and 
RCC staircases, structural bridges and concrete pathways with stainless steel 
hand rails and balustrades.<br>
<br>
All construction inside the cavern was done manually as no equipment and 
machineries could be taken inside because of the tough terrain.<br>
<img border="1" src="spl_feature5.jpg" align="right" width="280" height="194" hspace="5" vspace="2"><br>
Preparatory works, surveying and shop drawings of the entire project were 
completed before the tunnel breakthrough. Temporary guard ways and bridges were 
constructed parallel to the proposed permanent pathway for construction and 
material transportation. Excavation and material transportation were identified 
as the most critical activities. <br>
<br>
“During peak time, 200 workers were engaged in the project,” informs Mohapatro. 
During the construction, the cavern was flooded twice during the rainy season. 
Now, water sensors are fixed in the cave to warn of the danger level. The 
floodwater is drained through the natural siphon present inside the cave system, 
and dewatering pump. It is a perfectly sound system and confirms to the highest 
safety standards.<br>
<br>
Larsen &amp; Toubro is expected to soon hand over the site to the Ministry, 
signalling the formal completion of the project work. Cave managers will then 
come into play. And once that happens, cave tourism will take a big leap forward 
in Oman. For now, one waits with baited breath.</font></p>
