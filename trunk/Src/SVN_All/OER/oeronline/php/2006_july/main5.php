<b>New golf equipment kicks up a row<br>
</b>
</font><font face="Verdana" size="1">
<b><br>
</b><br>
<b>Do new golf equipment give an unfair advantage to some? Golf buffs debate the 
point<br>
</b><br>
<img border="1" src="golfupdate.jpg" align="right" width="150" height="174">The 
game of golf finds itself in the midst of a new row. Some of the recent 
technological advancements in golf equipment have set tongues wagging, 
literally. Do the new clubs and balls compensate for the lack of skill? That 
precisely is the crux of the debate. <br>
<br>
Mike Wyman, a recreational golfer, and the co-owner of the websites 
DateAGolfer.com and PuttingForPar.com, in a recent article, says he disagrees 
with this viewpoint. “The main people affected by the new technologies are the 
professional golfers themselves. I more than welcome any advancement that allows 
me to keep my ball out of the woods. Throw in a ball that I can hit even farther 
and I’m quite pleased. The problem, some say, is the professionals. The courses 
are tamed much easier than in the past and the equipment makes up for some lack 
of skill,” he says.<br>
<br>
Taking the debate forward, Wyman adds: “The truth is that as we move forward and 
make improvements on past products, we all move forward. Tiger Woods has access 
to the very same equipment that you or I can use as long as we want to pay for 
it. That levels the playing field in such a way as to eliminate any perceived 
unfair advantage Tiger has. The real show of skill is the ability to 
consistently play well, day after day, year after year using the same or similar 
equipment as everyone else.”<br>
<br>
What about the length of the golf courses? Wyman has his answer. “It again comes 
down the fact that when a competition is being held, all the golfers are playing 
the same venue. So, if in fact one golfer can reach the green on a Par 4 in one 
shot, so be it. That just shows he can hit the ball farther with more accuracy 
than his competitors using similar equipment.”<br>
<br>
In fact, it is likely that a more unfair advantage is allowed when drastic 
weather changes affect a tournament than equipment. Sometimes a 7am tee time is 
much better due to lack of wind than one at 3 in the afternoon. “Is it fair that 
the guy or gal teeing off at 3pm is forced to contend with a much stiffer wind?” 
Wyman asks. <br>
<br>
Wyman stresses that like with everything else, there is a limit and an extreme 
that will eventually come to a head. It wouldn’t be very enjoyable if the 
equipment gets so good that every professional golfer can consistently drive the 
ball onto a green 400 yards away. “I agree there should be some limits but we 
haven’t reached them yet and are a ways off from doing so,” he contends.<br>
<br>
So, are the new advances great for the recreational golfer? Wyman believes they 
are. “As a spectator, it is really something to watch a professional hit the 
ball so far. As a golfer, it is even more special to smack the ball 250 yards 
down the middle ourselves.”</font></p>
