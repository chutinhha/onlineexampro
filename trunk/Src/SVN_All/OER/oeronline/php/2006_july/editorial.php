    <p align="center" style="margin: 10" dir="ltr">
    <b><font face="Verdana" style="font-size: 16pt">Disruptive innovation for 
    value creation</font></b><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">“</font><font face="Verdana" size="2"><b>S</b></font><font face="Verdana" size="1">uccess 
    breeds active inertia, and active inertia breeds failure... Instead of 
    rushing to ask, ‘What should we do?’ managers should pause to ask, ‘What 
    hinders us?”<br>
    <br>
    Most corporates today face the challenge of identification of critical 
    success factors, which enable innovation to create substantial value. Many 
    times we find good companies failing because the strategic frames, 
    processes, relationships, and values that made them successful atrophy over 
    time into blinders, routines, shackles, and dogmas. These characteristics 
    not only hamper the ability to innovate within a company, but channel the 
    company’s energy and activity in the wrong direction when faced with 
    adversity.<br>
    <br>
    “Large, mature companies often lack the capabilities to be successful with a 
    disruptive product or service innovation*,” said Henry Birdseye Weil of MIT 
    Sloan School of Management. It happens, according to the management experts, 
    because these companies lose the ability to enter small and emerging 
    markets. Their values change and what was once an attractive opportunity now 
    looks “not big enough to be interesting.” They tend to forget that 
    disruptive innovations create new markets with different value propositions.<br>
    <br>
    With rapidly changing global marketplace, the knowledge and capability 
    advantages quickly get obsolete. During the period of disruptive innovation, 
    established companies are often left surprised by changes in the competitive 
    environment. They fail to understand who their key competitors are and how 
    customer needs, expectations and values have changed. Google’s incredible 
    success in a quick time, leaving giants, including Microsoft, completely 
    awestruck, is a classic example. Many a time, the big boys do not realise 
    that their knowledge and capabilities are becoming obsolete. It is often 
    said that markets become increasingly commoditized as they get mature. In 
    the process, the sources of sustainable advantage become less tangible, 
    e.g., intellectual property, know-how, information, brand, reputation, 
    relationships, trust, and the customer experience. Therefore, competing on 
    intangibles need different capabilities from competing on product or service 
    price and performance. <br>
    <br>
    Learning from the experience of companies managing to sustain their success 
    over a long period of time, management experts say there are three ways to 
    develop new capabilities — create new organizational structures within 
    current boundaries; spin out an independent organization; or acquire a 
    different organization. The required capabilities are determined by the size 
    and scope of the business, intensity of competition, and of course — the 
    disruptive impacts of innovation. “The availability of role models and 
    mentors, consequences of failure, status and quality of internal venture 
    leaders, and reinforcing network effects are critical success factors for 
    large companies attempting to turn innovation into value,” opined Weil. 
    Social attitudes have a major effect on the status of entrepreneurs, 
    consequences of failure, quality of people who lead ventures, willingness to 
    take risks, and the success rates of both start-ups and internal ventures in 
    large companies.<br>
    <br>
    Educational institutions such as universities often are the hub of 
    innovation clusters, e.g., MIT and Stanford in the US and Cambridge in the 
    UK. They can serve as incubators for innovative technology, entrepreneurs, 
    and start-up companies. At home, Sultan Qaboos University is beginning to 
    play that role but it has a long way to go. The launch of “TKM-Ernst &amp; Young 
    Big Business Idea Competition” organised by The Knowledge Mine, Ernst &amp; 
    Young and Information Technology Authority (ITA) is a good initiative to 
    identify and promote new innovative business ideas by fresh talent. If more 
    organizations come forward with such programmes, Oman could turn into a 
    hotbed of business enterprises with a new breed of entrepreneurs. <br>
    <br>
    The government also has a critical role to play to make disruptive 
    innovations turn into a value proposition for the companies. The government 
    policies impact the magnitude and accessibility of market opportunities, 
    e.g., through barriers to entry, regulation of competition, procurement 
    practices, establishment of standards, protection of intellectual property, 
    and advocacy of particular technologies. The government machinery needs to 
    be very careful not to make the process of turning innovation into value 
    more difficult through dysfunctional policies and interventions. Obstacles 
    to obtaining critical resources, development of new capabilities, market 
    entry or exit, start-up of new companies, formation of alliances, mergers 
    and acquisitions, establishment of standards, or adoption of new technology 
    weaken the virtuous dynamics and reduce the likelihood of success. <br>
    <br>
    *Disruptive innovation is a successfully exploited product, service or 
    business model that significantly transforms the demands and needs of a 
    mainstream market and disrupts its former key player e.g., CDs that have 
    disrupted the VHS market.</font></td>
