<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>DaimlerChrysler organises ‘Technician of the 
Year’ contest<br>
</b><br>
The DaimlerChrysler division of Zubair Automotive LLC organised a competition 
for choosing best automotive technicians in Oman at the Zubair Automotive 
Workshop-PDI Centre in Azaiba. Technician Jerome Mag-Iba was judged the first 
prize winner of the contest. “Oman is the only country in this region where the 
Technician of the Year Competition is held. A panel of five neutral judges who 
are technically knowledgeable and are staff of the different automotive 
divisions of Zubair Automotive evaluated the participating technicians. This 
event could turn out to be a regional competition in the future too.” explained 
Jim Greene, Senior Service Manager, Zubair Automotive LLC.</font></p>
