<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Monroe Reflex for Maximum Safety<br>
</b><br>
<img border="0" src="autonews_monroe.jpg" align="right" width="150" height="56">Monroe, 
the leading shock absorbers brand, has introduced a new product called “Monroe 
Reflex” which promises to bring maximum safety through greater stability and 
control without sacrificing comfort. The introduction of the Monroe Reflex is 
also an important step in helping consumers ‘drive safe’ through proper 
maintenance of a vehicle’s ‘Safety Triangle’ chassis system. The Safety Triangle 
is the system of interconnected components that help enhance driving performance 
and safety through proper steering, stopping and stability characteristics. 
Towell Auto Centre is the dealer for Monroe products in the Sultanate.</font></p>
