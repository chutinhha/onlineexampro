<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Nissan Tiida Hatchback gets SINA award</b><br>
<img border="1" src="autonews_nissan_tiida.jpg" align="left" width="100" height="105"><br>
Nissan’s Tiida Hatchback has been awarde the “Car of the Year 2006” by SINA 
Corporation, a leading Asian online media company and information service 
provider based in China. This is the third honour for this Japanese compact car 
which comes in both sedan and hatchback versions. It has also won awards for 
‘Best Exterior Design and Best Performance for Value’. In Oman, Suhail Bahwan 
Automobiles LLC exclusively sells the Nissan brand.</font></p>
