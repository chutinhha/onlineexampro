<font face="Verdana" size="1"><b>Lexus introduces new 2007 ES350<br>
</b><br>
<img border="1" src="autonews_lexus.jpg" align="left" width="150" height="116"></font><font face="Verdana" size="2"><b>L</b></font><font face="Verdana" size="1">exus 
has launched the new 2007 ES350 in Oman. ES350 is powered by a 3.5 L, 300 HP, V6 
engine with Dual VVT-i and packs in a whole array of luxury and safety features, 
like Mark Levinson Audio System, DVD Navigation, Smart Entry &amp; Start System, 12 
airbags and Intelligent Adaptive Front Lighting System (AFS) etc. <br>
<br>
The exterior has undergone a spectacular transformation. With sleek, 
sophisticated lines, a wider body, a longer hood, an extended wheelbase, and a 
cabin that seems to melt gracefully into the vehicle’s shortened rear deck, the 
ES 350 now dons a new look. It’s a departure from traditional styling, and a 
bold one for that matter. <br>
<br>
The new car hasn’t grown in overall length, but its wheelbase is up 55 mm over 
the previous ES. The ES 350 is fitted with four-wheel disc brakes with ABS, 
Brake Assist and Electronic Brakeforce Distribution. There’s real walnut trim in 
the center console and door panels, and on the steering wheel. For navigation 
assistance, there is the Lexus DVD Navigation System providing Arabic &amp; English 
Voice guidance – across Oman and the GCC. It comes with a high-resolution screen 
and 32,000 colours to make finding your destination even easier. The Navigation 
Screen also doubles up as a DVD player and a Colour Back View Monitor with 
obstacle location is also integrated into it.</font></p>
