<font face="Verdana" size="1"><b>Mazda bags ‘World Car of the Year 2006’<br>
</b><br>
<img border="1" src="autonews_mazda.jpg" align="right" width="100" height="74">The 
Mazda MX-5 has been among the top three finalist of ‘2006 World Car of the Year’ 
during the New York International Auto Show 2006. A jury of 46 international 
automotive journalists reviewed every new car available around the world and, in 
an exhaustive selection process, narrowed them down to three- Mazda MX-5, BMW 3 
series and Porsche Cayman S.</font></p>
