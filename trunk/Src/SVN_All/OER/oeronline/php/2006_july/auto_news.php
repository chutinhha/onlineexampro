<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Hyundai becomes sixth largest automaker<br>
</b><br>
<img border="1" src="autonews_hyundai.jpg" align="left" width="150" height="105">Detroit-based 
weekly Automotive News, according to most recent survey of global automotive 
sales and production, classed Hyundai Automotive Group as the world’s sixth 
largest automotive manufacturer in the world. Hyundai Motor Co. registered an 
11.6 percent jump in global sales (3,715,095 cars) in 2005 which was the largest 
percentage gain of any carmaker in the million-plus sales category. <br>
<br>
Hyundai is an ‘Official Partner’ for the 2006 FIFA World Cup. OTE Group is the 
exclusive dealer of Hyundai’s range of vehicles in Oman. Recently, OTE Group of 
Companies and Hyundai sent 10 FIFA package contest winners from Oman to an all 
expenses paid trip to Germany.</font></p>
