<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Seat Leon launched</b><br>
<br>
<img border="1" src="autonews_seat.jpg" align="right" width="150" height="80">SEAT 
has introduced its new generation model, the new Leon with a 2.0 FSI engine. 
With a sporty and innovative design, the latest Leon is also the most powerful 
model to come out of the Leon line. The new model has a body that neatly blends 
the sporty image of a stunning coupé with five-door practicality. As for active 
safety, this version also includes as standard an anti-lock braking system with 
electronic brake power distribution, TCS traction control system and ESP 
Electronic Stability Program. Seat is a Spanish company owned by the German 
Volkswagen Group. In Oman, Towell Auto Centre represents Seat.</font></p>
