JVC’s Widescreen HD-ILA TVs <br>
</b><br>
<img border="0" src="marketwatch_2.jpg" align="right" width="150" height="126">JVC 
has introduced its new line-up of widescreen HD-ILA televisions in Oman. The 
statuesque HD-Z70RX5 and HD-Z56RX5 promises visual entertainment on a grand 
scale as they feature D-ILA (Direct Drive Image Light Amplifier), a reflective 
technology that delivers exceedingly high levels of brightness even with very 
large screen sizes. And thanks to D-ILA, these remarkable HD-ILA televisions 
boast sophisticated LCOS pixels that require virtually no space between each 
pixel. This makes it possible for D-ILA to yield the highest aperture ratio 
(reflective area) and maximize the total area in which mega pixels can be 
positioned, resulting in seamless and exquisite image reproduction unhindered by 
the “screen-door effect”. D-ILA also employs 3-chip LCOS technology to eliminate 
the “rainbow effect” that can result from using single chip, colour-wheel 
