<font face="Verdana" size="1"><b>BMW 3 Series named ‘World Car of the Year 2006’<br>
</b><br>
<img border="1" src="autonews_bmw.jpg" align="right" width="175" height="87">The 
new BMW 3 Series has been named ‘2006 World Car of the Year’ during the New York 
International Auto Show 2006. A jury of 46 international automotive journalists 
reviewed every new car available around the world and, in an exhaustive 
selection process, narrowed them down to three — the BMW 3 Series, Mazda MX-5 
and Porsche Cayman S. The 3 Series was awarded the prestigious World Car of the 
Year honour for its balance of ride, handling, performance and interior space. 
“The BMW 3 Series has been one of the world’s most enduringly popular cars since 
BMW pioneered the sports sedan concept more than 40 years ago” said Guenther 
Seemann, Managing Director, BMW Middle East.</font></p>
