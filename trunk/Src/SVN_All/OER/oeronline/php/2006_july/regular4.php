Diamond series, based on the 2007 model year Mulliner Driving Specification, 
boasts of unique features like carbon ceramic brakes and 20-inch, 14-spoke, 
alloy wheels. The coupe is the most successful car in Bentley’s history. Just 
400 units of the Continental GT will be available for worldwide distribution.</font></p>
