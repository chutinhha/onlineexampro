<font face="Verdana" size="1"><b>2007 Cadillac Escalade<br>
</b><br>
<img border="1" src="autonews_cadillac.jpg" align="right" width="175" height="118">Cadillac 
recently unveiled the 2007 Escalade next-generation luxury SUV at the BIEL in 
Beirut. The familiar yet refreshing form of the Escalade introduces the next 
step in Cadillac’s ongoing renaissance. With a new 6.2L all-aluminium V-8 engine 
with Variable Valve Timing technology, the Escalade covers a speed of 0-100 km/h 
in 6.8 secs. It features new power train, chassis, safety and interior systems. 
This model will be available in the Middle East region from May onwards, with 
extended-wheelbase Escalade ESV and Escalade EXT models to be introduced later 
this year.</font></p>
