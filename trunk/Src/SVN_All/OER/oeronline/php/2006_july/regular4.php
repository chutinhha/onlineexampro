<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Jaguar XK sports car launched</b><br>
<br>
<br>
<img border="1" src="autonews_jaguar1.jpg" align="left" width="78" height="101">Mohsin 
Haider Darwish LLC (MHD) recently unveiled the all-new Jaguar XK sports car in 
Oman. With a beautiful and contemporary design, the XK is the first of a new 
generation of cars that will offer the best of everything Jaguar represents. XK 
boasts of 4.2 litre AJ-V8 engine and reaches 60mph from standstill in 5.9 
seconds with a top speed of 155mph. The new XK has an aluminum monocoque 
structure and a 10 percent improvement in power to weight ratio. Excerpts of 
OER’s chat with Andy Gawthorpe, MD (Jaguar &amp; Land Rover) Middle East &amp; North 
Africa, Premier Automotive Group:<br>
<b><br>
How important is the launch of new XK for Jaguar? <br>
</b><br>
The launch of the all-new Jaguar XK marks an exciting new era for Jaguar in 
terms of both design and engineering, and reinforces the marque’s reputation for 
building ground-breaking cars that are as rewarding to drive as they are 
gorgeous to look at. The new XK has been engineered above and beyond the high 
expectations that customers rightly have for a Jaguar sports car, a fact made 
possible because everything from its advanced aluminium chassis to its 
sophisticated transmission and exquisite interior has been designed in pursuit 
of a luxurious, advanced and stunning new Jaguar.<br>
<b><br>
What makes new XK so unique?<br>
</b><br>
The new XK offers the customer a sporty drive without compromising on luxury 
while its superb performance and handling will allow it to be bought by 
customers who previously would only have considered outright sports cars which 
compromised luxury and space in favour of more aggressive handling 
characteristics.<br>
<b><br>
Who are going to be your primary customers for new XK?<br>
</b><br>
<b>
<img border="1" src="autonews_jaguar2.jpg" align="right" width="175" height="118"></b>The 
new XK will appeal to a select group of affluent potential purchasers, who have 
an affinity with luxury products. They are looking for a grand tourer as well as 
a car with pure sporting credentials, one that provides exhilaration and 
escapism combined with status and exclusivity – ‘luxury in a sports skin’. 
Consequently, the new XK’s beautiful exterior styling — which echoes Jaguar’s 
lineage and signals performance — meets exactly those requirements. Advanced 
technology that adds to the driving experience will attract the ‘savvy’ 
consumers looking for features such as Adaptive Cruise Control and a 
touch-screen information and control centre. In short, the all-new XK offers 
customers the best of both worlds — sport and luxury, versatility and 
capability.</font></p>
