<font face="Verdana" size="1"><b>Mazda’s Zuper Zoom road show<br>
</b><br>
Towell Auto Centre, as part of a special summer marketing campaign, is running 
an extensive nationwide road show. In four weeks, Mazda’s new exhibition unit 
will visit locations including Muscat, Nizwa, Ibri, Barka, Sohar, Sur, and Bani 
Bu Ali. The entire sporty range of Mazda cars will be on display such as Mazda3, 
Mazda6, Mazda6 2.3 MZR to the multi purpose Mazda B Series. Live DJ will entice 
visitors with the Zuper Zoom music rocking the place and the Mazda brand vision 
playing on screen. Mazda sales staff and promoters will provide an on the spot 
test-drive and impart information and technical specifications on any of the 
Mazda vehicles. Special gifts will be given to those who will take a test drive.</font></p>
