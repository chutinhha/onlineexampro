</font><b>
		<font face="Verdana" size="4">Kempinski set for 2010</font></b><font face="Verdana" size="1"><br>
		<br>
<img border="0" src="face2face05.jpg" align="right" hspace="8" style="border: 2px solid #EFEFE9; " vspace="4"><b>The 
		Wave has tied-up with 110-year old German hotel chain, Kempinski, to 
		manage a five-star property, to be built at an estimated cost of 
		US$100-120 million. Ulrich Ec</b><b>khardt, Senior VP – Middle East &amp; 
		Africa, Kempinski Hotels, talks to OER about the company’s foray in Oman 
		and its global operations</b><br>
		<br>
		<b>What does Kempinski as a brand and company stand for?</b><br>
		Kempinski Hotels is the oldest luxury hotel chain in the world and the 
		only truly European luxury hotel group, having acquired its first 
		property in 1897. Kempinski Hotels, is trade name for Kempinski 
		Aktiengesellschaft, an independent German delisted AG, which is involved 
		in a number of hotel and hospitality related businesses, including 
		conferencing, catering and hotel supplies. <br>
		<br>
		Kempinski Hotels now owns and operates an international portfolio of 59 
		hotels that reflect the finest traditions of European hospitality. It 
		also has the largest portfolio representation in leading hotels of the 
		world. A further 41 hotels are either under final development or 
		construction in Europe, the Middle East, Africa, Asia, and Latin 
		America. In Germany, Kempinski owns the Hotel Vier Jahreszeiten in 
		Munich and operates a number of properties, including the famous Hotel 
		Adlon in Berlin, the Hotel Atlantic in Hamburg and the legendary Resort 
		Hotel Heiligendamm in Baltic Sea. Among its international properties are 
		such legendary names as Istanbul’s Ciragan Palace Hotel, the Emirates 
		Palace in Abu Dhabi, the Grand Hotel des Bains in St. Moritz, and the 
		Hotel Baltschug in Moscow.<br>
		<br>
		<b>What made Kempinski make an entry in Oman?</b><br>
		We always wanted to be present in Oman. I’ve been to Oman couple of 
		times scouting for business opportunities. We understand that Oman has 
		been one of the most desired destinations for Germans and tourists from 
		Central Europe. When we worked with UAE-based Majid Al Futtaim Group on 
		the Mall of Emirates in Dubai, we established good business relationship 
		with them. We were interested in exploring more business opportunities 
		with them. As they are the co-owners of The Wave project, we got our 
		opportunity as they were looking for leading hotel chains to manage 
		their planned multiple hotels at The Wave.<br>
		<br>
		<b>What will be the size and design of Kempinski property at The Wave?<br>
		</b>We are expecting it to be a 250-300 room property. It will be 
		operational by 2010. We are also in discussions to run some of the 
		residences, including villas, as well. Kempinski always lets the 
		developer of the property to decide on the design and architecture. We 
		play an advisory role in the process. I expect the hotel at The Wave to 
		be based on traditional Omani design, with the modern international 
		standards.<br>
		<br>
		<b>With more hotel properties coming up by 2010 in and around Muscat, do 
		you think there will be enough business for you?<br>
		</b>It is not a question of demand, it is a question of preference. We 
		are clearly the most identifiable name in the hotel business in Europe. 
		As Europeans are the number one visitors to Oman, we will have instant 
		brand recognition. We will certainly command our own customer database, 
		irrespective of the number of hotels that come up in Muscat.<br>
		<br>
		<b>What kind of guests you will be expecting?<br>
		</b>Traditionally we are a city hotel company. We have many resorts but 
		our strength lies in the city business hotel operations. Most of our 
		business generally comes from the corporate segment, including corporate 
		leisure, MICE groups and individual travellers. We are not into the mass 
		market. We expect a similar profile of guests in Muscat.<br>
		<br>
		<b>Are you looking for business in Oman beyond The Wave?<br>
		</b>At one stage, we were actively looking at a property in Salalah but 
		it didn’t work out due to certain reasons.<br>
		<br>
		<b>How do you make money through say a hotel in The Wave?<br>
		</b>We get management fees. The details are confidential and can’t be 
		revealed.<br>
		<br>
		<b>Do you get incentives as well?<br>
		</b>Yes, in fact, our real income comes through incentive money.<br>
		<br>
		<b>What is your strategy for growth in Middle East and Africa?<br>
		</b>We are the fastest growing hotel group in Africa. We have five 
		hotels there. We plan to have 12 overall in the next two-and-a-half 
		years. In the entire Middle East and Africa, we are looking at 27 hotels 
		by 2010. Sixty per cent of them will be in the Middle East and the 
		balance in Africa. </font></p>
