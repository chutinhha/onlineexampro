		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><b><font face="Verdana" size="4">Put on your ‘maverick’ boots</font></b><font face="Verdana" size="1"><br>
        <b>Challenging conventional wisdom may be the defining spirit for the 
        next era of business leadership. William C Taylor and Polly LaBarre 
        profile such challengers in a book that’s ‘unputdownable’</b><br>
        <br>
        <img border="0" src="corner.jpg" width="150" height="227" align="left" hspace="6" vspace="4">There 
        are books. And books. Some shake you up while some put you to sleep. So 
        one needs to be choosy while shopping for books&#8722;particularly on 
        management. Lot of books that need not have been written in the first 
        place often occupy prime shelf space because everything is ‘business’ – 
        read money-making. Original writing is rare. Yet, I would recommend 
        ‘Mavericks at Work’ by William C Taylor and Polly LaBarre. Not because 
        the cover jacket has the management guru Tom Peters uttering ‘I didn’t 
        read this book. I devoured it.’ Despite Peters, who has become jaded of 
        late.<br>
        <br>
        The authors’ tagline ‘Why the Most Original Minds in Business Win’ gives 
        it all. What makes the book outstanding is the presentation that is 
        racy, breezy and chatty without losing the focus. One feels as if one 
        was racing through a screenplay. Hats off to the authors: Taylor is the 
        founding editor of talked-about business magazine, Fast Company and 
        LaBarre his editorial colleague.<br>
        <br>
        What are the takeaways from this book? Unhesitatingly and 
        unapologetically I will reproduce the blurb: being different makes all 
        the difference; sharing values beats selling value; the company with the 
        smartest customers wins; nobody is as smart as everybody; character 
        counts for as much as credentials; great leaders are insatiable 
        learners.<br>
        <br>
        The book is so enticing with anecdotes full of life and verve. However, 
        my favourite quote in the book comes in the chapter on ‘People and 
        Performance’. “You are responsible for your career… You need to look at 
        it like a business. What do you want to happen? How do you want to be 
        known? We encourage and reward people for moving around, and not just 
        within one area. You can stay in one place and become an expert and a 
        leader. You can have a good career that way. But a great career is all 
        about movement,” says the Chief of SEI, a company touted by Fortune as 
        one of the ‘100 Best Companies to Work For’.<br>
        <br>
        The book profiles 32 diverse and inspiring organisations, some with as 
        few as a hundred employees and millions in sales and others with 
        thousands of employees and billions in sales.<br>
        <br>
        If you are a busy executive with time deficit to read through this 
        profound book, relax. Just read the crispy Maverick Messages (Chapter 3 
        on Sizing Up Your Strategy; Chapter 6 on Open-Minding Your Business; 
        Chapter 9 on Building Your Bond With Customers; and Chapter 12 on 
        Practicing Your People Skills) running into not more than 35 odd pages. 
        Another interesting aspect of this book is that you can begin reading 
        from whichever chapter that interests you. Yes, they are standalone 
        takeaways. By and large, management books have a long-winding Appendix 
        that I refuse to look at. Here is an exception. The section is packed 
        nicely and very informative.<br>
        <br>
        The Tayor-LaBarre duo, in the Introduction, claim that Mavericks at Work 
        is more than a how-to-book. It is also a what-if-book. Yes. What if I 
        have not picked up the book at Turtles? Grab a copy N-O-W. You will not 
        regret. By the way, what is your offering, Taylor?</font></p>
        <p align="justify"><font face="Verdana" size="1">– <b>Ramesh Kumar </b>
        <br>
        <br>
