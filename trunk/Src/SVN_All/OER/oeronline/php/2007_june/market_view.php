<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Bulls, ahoy</font></b><font face="Verdana" size="1"><br>
<b>MSM is expected to continue to trade positively, supported by investors’ 
positive outlook on corporate earnings and the healthy liquidity position</b><br>
<br>
The bulls are back, storming the Muscat Securities Market (MSM) after a short 
lull seen in April-end. Overall, the MSM performed strongly in April-May 
supported by buoyant Q1 earnings. The MSM 30 index composite gained 266 points, 
i.e., 4.68 per cent to close at 5950 points. All sectors, except the industrial 
sector, registered gains, with the banking sector leading the way with a 4.77 
per cent return. Overall activity increased sharply, with the MSM seeing a 46 
per cent increase in total turnover to RO 198.9 million as compared to the 
March-April period.<br>
<br>
Cement stocks also witnessed some activity, with Oman Cement Co. clarifying 
about its proposed expansion plan for clinkering capacity, which is expected to 
be complete by 2010. Market participants appeared to be wary about the company 
missing the current boom in construction in northern Oman with this belated 
capacity addition. Renaissance Services announced that together with its local 
partner in Kazakhstan, it has been awarded three prestigious contracts valued at 
US$350 million (RO 135 million) by Agip Kazakhstan North Caspian Operating 
Company N.V. (Agip KCO) in Kazakhstan for the provision of up to 15 special 
purpose ice class barges.<br>
<br>
The period also witnessed unsuccessful negotiations between Oman United 
Insurance Co. and ONIC Holding on merging OUIC with the latter’s Al Ahlia 
Insurance Co. The first week of May witnessed another interesting event, when 
BankMuscat made an share swap offer to acquire Alliance Housing Bank, valuing 
the latter’s shares at RO0.375, a 34 per cent premium to the then existing 
market value. The board of directors of AHB rejected the offer, saying that they 
were considering many other options. However, the market seemed to have 
appreciated the valuation done by BankMuscat and the AHB stock subsequently 
witnessed a huge run up.<br>
<br>
The market is expected to continue to trade positively in May-June, supported by 
investors’ positive outlook on corporate earnings and the healthy liquidity 
position. However, beyond June, the normal lacklustre trading and low buying 
interest may set in. Also, towards the end of June, the IPOs of Galfar 
Engineering and Oman Oil Marketing are expected to open and they may absorb some 
liquidity from the secondary market.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
