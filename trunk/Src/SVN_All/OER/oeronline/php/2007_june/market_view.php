		<font face="Verdana" size="1"><br>
		<b>GCC Markets<br>
		</b>Encouraging trends were also witnessed on the other GCC bourses. All 
		market indices closed in the positive territory for the period ended 
		mid-May, with Doha stock market being highest gainer registering a 17.2 
		per cent increase. Again, the gain was mainly due to strong Q1 results, 
		with the banking sector taking the lead. It was followed by the UAE 
		markets, with the Dubai Financial Market (DFM) increasing by 10.6 per 
		cent and the Abu Dhabi Securities Market (ADSM) moving up by 9.7 per 
		cent. The gains were chiefly due to strong buying in bank and other blue 
		chip stocks from the utility and transportation sectors. The 
		year-to-date returns for ADSM stood at 11.6 per cent, the highest among 
		other indices in the GCC region. The Kuwait markets too witnessed a 
		bullish trend and closed on a positive note. It reported a rise in 
		volumes on account of increased buying of low priced stocks for quick 
		gains. On the flip side, the Saudi market (TASI) closed in the red on 
		account of poor Q1 numbers. <br>
		<br>
		Key market participants observe that buying interest has started 
		emerging in markets like UAE, Qatar and Kuwait. Saudi Arabia still reels 
		under the valuation conundrum and issues related to deregulation, and is 
		expected to see a delayed recovery. Though the liquidity position 
		remains buoyant in most markets, weakness in the US dollar has been 
		adding to inflationary pressures. First quarter earnings announcements 
		from companies have by and large been encouraging which is again a 
		positive cue.<br>
		<br>
		<b>IMF Report on Global Economy<br>
		</b>In its latest report on the world economy, issued in April, the 
		International Monetary Fund has said that the global economy would 
		continue to post robust growth in 2007 and 2008, growing by 4.9 per cent 
		each year. This outlook is interestingly important as the growth in the 
		United States is expected to come down to 2.2 per cent from 3.3 per cent 
		in 2006 due to poor performance of the housing sector. Furthermore, 
		growth in Euro zone is expected to ease in the current year, reflecting 
		the effect of a changing monetary policy. Japan and the developing 
		countries will lead the growth in the coming two years benefiting from 
		continued higher commodity prices.<br>
		<br>
		According to the IMF, the risk factors are less threatening now compared 
		to six months ago. A key question in assessing the risks is whether the 
		global economy will be able to decouple from the US where growth was 
		slower than expected. <br>
		<br>
		The Middle East economies grew strongly in 2006 and are expected to grow 
		well in the coming years supported by high oil revenues. However, 
		current account surpluses to GDP are expected to decline due to 
		declining oil revenues and higher imports funding. Private investment is 
		a key factor to sustain economic growth and create more job 
		opportunities in the region. The ambitious US$700-billion investment 
		plan for 2007-2010 should help the region’s economies to advance on 
		diversification and contain the rapidly growing labour market, the 
		report says. <br>
