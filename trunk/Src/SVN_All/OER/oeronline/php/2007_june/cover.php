<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1"><b>Note: The details of size and instruments of 
fund-raising have been based on market sources data and are subject to change.</b></font></p>
<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1">After Galfar, it could be the turn of Takamul – a still-to-be-formed investment 
arm of government-owned Oman Oil Company. This company will promote secondary 
downstream industries, specially those units using polypropylene, paraxylene, 
benzene and aluminium. Takamul will have a paid-up capital of RO50 million and 
an authorised capital of RO100 million. The promoters will hold 60 per cent and 
the rest – RO20 million – will be raised from the public. And like Bank Sohar – 
which raised RO20 million through an IPO earlier this year – Takamul does not 
conform to the CMA’s stipulation of continuous operations for three years before 
going public. But Abdullah bin Salem Al-Salmi says under certain unique 
conditions in Oman, some companies’ IPOs are allowed on a case-to-case basis. 
And there’s no doubt among brokers and investors, Takamul’s IPO will be a huge 
success.<br>
<br>
Apart from the Takamul IPO, Oman Oil Company is also seen raising funds by 
divesting its stake in its flagship, Oman Oil Marketing Company (OOMC), which is 
ranked seventh in OER Top 20. “Oman Oil currently holds about 41 per cent in 
OOMC. It is likely to retain 24 per cent and divest the rest to the public. OOC 
is likely to raise about RO13 million through this divestment,” says Sankar.<br>
<br>
Adding to these, at least two banks are also seen tapping the capital market 
later in the year or early next year. “While BankMuscat is most likely to raise 
about RO100 million, Oman Merchant Bank will offer RO50 million worth of 
shares,” say sources. “Actually, BankMuscat has plans to raise about US$500 
million (RO200 million). It might raise about RO100 million in Oman and the rest 
as GDRs (global depository receipts). The proceeds are likely to be used for its 
international operations,” they add.<br>
<br>
On its part, the newly formed Oman Merchant Bank – promoted by Dubai-based Gulf 
Merchant Bank – is likely to raise about RO50 million. The bank has an 
authorised capital of RO100 million, the minimum that new banks in Oman have to 
have.<br>
<br>
“No doubt these are some of the highly anticipated issues. But we also look 
forward to smaller issues such as those of Sohar Power. This IPO is expected 
soon, and is likely after the government meets certain obligations as part of 
its agreement with the WTO. The details are still awaited,” says Jesrani. <br>
<br>
Besides, the market could also witness some funds and rights issues. Vision has 
already started its open-ended Emerging Oman Fund, with a minimum size of RO2 
million. “The primary objective of the fund is to achieve growth through capital 
appreciation and dividends by investing in equities listed and/or to be listed 
on the MSM,” the company says in its offer document. On the rights issues front, 
Al Omania Financial is raising about RO8 million. GIS too plans to come out with 
an RO3-million rights issue while Salalah Flour Mills is finalising the details 
of its issue, with market sources saying that the company is planning to raise 
half a million riyals. </font></p>
