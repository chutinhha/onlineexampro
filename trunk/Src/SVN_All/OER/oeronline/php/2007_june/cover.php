<font face="Verdana" size="1"><br>
</font><font size="4" face="Verdana"><b>MSM hots up</b></font><font face="Verdana" size="1"><br>
<b>Omani companies are lining up to raise RO400 million from the securities 
market in the coming months. With the MSM index crossing the 6k mark in May, the 
market in Oman is unlikely to be the same again. P Aneel Kumar finds out what 
makes the MSM attractive for companies as well as investors, and looks at the 
companies planning to come up with fresh offerings </b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 5px">
<img border="0" src="cover1.jpg" width="400" height="241"></p>
<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1">As the mercury soars at the beginning of another sweltering summer, heat of a 
different kind is on the rise on the floor of the Muscat Stock Market. The 
Market is gearing up for action in the coming days, especially with the Capital 
Market Authority announcing recently that at least four public issues will see 
light in 2007. Without doubt, trading on the MSM is not going to be the same.<br>
<br>
The market index, which recently soared past the 6,000-point mark, might achieve 
even greater heights if its breadth and depth are expanded and that is exactly 
what is going to happen in the coming days. Though the CMA has talked about only 
four IPOs in 2007, market sources believe that at least seven to eight companies 
will take the primary market route to raise funds. Thus, the total size of this 
fund-raising activity could breach the magical RO400-million mark. If this 
happens – and there are no reasons why it should not – then the Sultanate can 
easily enter the big league of nations whose capital markets, by virtue of their 
hyper activity, mirror their economic growth.</font></p>
		<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1"><b>The Growth Index<br>
</b>Traditionally, as the numbers suggest, the capital market in Oman has not 
mirrored the nation’s booming economy. While the Sultanate’s nominal GDP grew at 
a rate of 17 per cent in 2006, it was even higher at 24 per cent in 2005. 
Against this, the MSM 30 Index registered an increase of 14.5 per cent at 
5,581.57 points at the end of 2006 up from 4,875.11 points a year ago. 
Similarly, market capitalisation registered a 5.8 per cent growth in 2006 at 
RO6,220.80 million as against RO5,878.50 million in 2005. “At 32 per cent, the 
market capitalisation-to-GDP ratio in Oman is amongst the lowest in the world. 
In the developed countries, this ratio is as high as 110-130 per cent. With 
these new public offers, things might change for the good, making the MSM 30 
Index truly reflective of the Sultanate’s economy,” says Sankar Kailasam, 
vice-president (research) at Gulf Investment Services Company.<br>
<br>
The main reason for the MSM 30 not being truly reflective is that while the 
Sultanate’s economy is primarily based on the oil and gas sector, there are 
virtually no oil and gas companies listed on the MSM. “It is for this reason 
that we are encouraging more and more family owned, limited liability companies 
to go public. Many companies like Galfar are now coming up with public issues,” 
says Abdullah bin Salem Al-Salmi, chairman, Muscat Securities Market.<br>
<br>
And his efforts are bearing fruit, if one considers the growing number of 
companies that are approaching the CMA for raising funds and that too with issue 
sizes that have not been seen earlier.<br>
<br>
For obvious reasons though these companies do not want to talk about their 
offerings. But prospective investors are already readying themselves to lap up 
the forthcoming opportunities. But what are these opportunities?<br>
<br>
According to market insiders, the size of this opportunity could be in the range 
of RO300-400 million. The duration: next 12 months. The players: About 6-8 
companies, including Galfar, Oman Oil, Takamul, Oman Merchant Bank, Vision Fund, 
Al Omania Financials, Gulf Investment Services, Sohar Power, BankMuscat, Salalah 
Flour Mills and Qatar-Oman Investment Company. Expected response: 
oversubscription by a good margin. Initial response: surprise.<br>
<br>
Yes, the market has been taken by surprise over the planned fund-raising moves. 
“Generally, the summers are very dull in Oman as far as activity in the market 
is concerned. But it is surprising to see so much action this summer,” says Hari 
Kumar Varma, vice-president (research) at Vision Securities. But, why the sudden 
buzz in action?</font></p>
		<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1"><b>Touch-Free Market<br>
</b>The Sultanate’s securities market can be best described as a ‘touch-free 
market.’ Even when global indices, including those of the GCC markets, were 
heading south, the MSM 30 Index was growing steadily. “This market has a history 
of giving average returns of around 14 per cent. The size of the market too is 
growing steadily. Valuations and fundamentals are really good. At the macro 
level, oil prices are likely to sustain at higher levels. These are all positive 
signs for the markets in medium to long term, which these companies want to take 
advantage of,” says Ritesh Jesrani, head of research at Al Madina Financial &amp; 
Investment Services Company.<br>
<br>
Fuelling this is the overall boom that the Sultanate’s economy is witnessing. 
“As the economy grows faster, companies need more investments to cash in on the 
boom. For example, the construction and engineering company, Galfar Engineering 
and Contracting, is going public with its eyes set on huge infrastructure 
projects that the government has lined up. It will be easier for the company to 
bag big-ticket projects if it is listed,” says M Sundararajan, executive 
vice-president &amp; head of corporate finance at Fincorp, the lead manager for the 
Galfar IPO.<br>
<br>
While Sundararajan did not divulge details about the Galfar IPO, market sources 
believe the issue size will be in the range of RO60-70 million. “This offering 
has been in the offing for quite sometime. It is going to be one of the biggest 
IPOs after those of Omantel and Bank Sohar. The promoters are likely to offer 40 
per cent of the company’s capital. The issue might also be opened to other GCC 
nationals, so the response is likely to be overwhelming,” says Jesrani. Galfar 
is a construction company with a total workforce of 23,000, revenues in excess 
of RO260 million and branches in Brunei, the UAE and Qatar.</font></p>
