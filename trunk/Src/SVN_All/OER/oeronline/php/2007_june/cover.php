		<p align="justify" style="margin-left: 10px; margin-right: 5px">
<font face="Verdana" size="1"><b>Widening Horizons<br>
</b>Apart from these issues, other investment doors are also opening up for Omanis. 
For example, the newly formed Qatar and Oman Investment Company – with a capital 
of US$82.5 million – is likely to get listed on the Doha Securities Market. The 
promoters (Qatar Investment Authority and the Government of the Sultanate of 
Oman) have subscribed to 12 million shares, while the balance – 18 million – 
shall be offered to the public, equally in Qatar and Oman.<br>
<br>
In all, as the MSM chairman puts it, it is a win-win opportunity for all 
players. But the real question still remains. Does the market have the capacity 
to absorb these issues? “It is a fact that of late the markets have not been 
performing well. For instance, the average return so far in 2007 is just about 
5-6 per cent. Some of the bad experiences of Omantel IPO are still fresh in the 
minds of investors. But still, the valuations and fundamentals are very good. 
There might be some problem with regard to liquidity, but if properly managed, 
this short-term crisis can easily be overcome,” says Jesrani.<br>
<br>
Whatever may be the expectations or anticipations of the players, the euphoria 
in the market is quite apparent. “More than US$26 billion worth of projects have 
been announced in Oman in 2007. The returns on MSM for the last five years have 
surged to 267 per cent. Why don’t we take advantage of this boom?” asks a 
banker.</font></p>
