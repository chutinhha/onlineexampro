<b><font face="Verdana" size="4">Romancing the wheel</font></b><font face="Verdana" size="1"><br>
<b>Tyres have an important bearing on the performance and safety of
the car. Follow these tips to get the most out of your tyres</b><br>
<br>
<img border="0" src="spot1.jpg" width="250" height="142" align="left" hspace="6" vspace="4">Any tyre, no matter how well constructed, may fail in use as a result of 
puncture, impact damage, improper inflation or other conditions resulting from 
use. Tyre failures, in turn, may result in damage to the vehicle or personal 
injury. To minimise risk, the following maintenance tips should be followed:<br>
<br>
<b>Inflation pressure</b><br>
Tyre pressures, including the spare, should be checked at least once a month, 
before driving when the tyres are cold (after the vehicle has been stopped for 
at least three hours or driven for less than 1.6 km). Maintaining the 
recommended tyre pressure provides proper sidewall deflection and safe operating 
temperatures for the tyres. It is also very important for the tyre’s life, 
safety and vehicle handling. Under-inflation creates excessive heat, lower load 
carrying capacity and can seriously reduce the tyre’s life. Irregular wear can 
also be caused by under or over-inflation. Use dust caps on valves to maintain 
an airtight seal while keeping dust and moisture out.<br>
<br>
<b>Tyre rotation for passenger cars</b><br>
The purpose of rotating tyres is to achieve a more uniform tyre wear for all 
tyres on a vehicle. Before rotating tyres, determine the cause of any unusual 
wear and correct any misalignment, imbalance or other mechanical problems. The 
first rotation should be after 5,000 to 7,500 km and every subsequent rotation 
should be after every 10,000 km. Do not mix radials and bias ply tyres on the 
same vehicle. Do not mix or rotate different speed rated tyres.<br>
<br>
<b>Wheel alignment and balancing <br>
</b>Both wheel alignment and balancing are very important considerations for 
deriving safety and maximum mileage from your tyres. Check how your tyres are 
wearing out at least once a month. If your tyres are wearing out unevenly, such 
as the inside shoulder is wearing out faster than the rest of the tread, your 
car may be out of alignment. This condition not only reduces the life of your 
tyre but also adversely affects the handling characteristics of your car, which 
could be dangerous. An imbalanced tyre and rim assembly may also be a cause of 
vibration at high speeds. Prolonged use of the tyres in such a condition can 
lead to tyre damage.<br>
<br>
<b>Tyre replacement</b><br>
There are Tread Wear Indicators (TWI) on the sidewall of your tyre. These 
indicators are at a depth of 1.6 mm. When the tread of your tyre gets worn out 
due to use and comes down to the level of these indicators, it is an indication 
to replace your tyre. When your tyre gets damaged due to sidewall cuts, tread 
cuts, damaged beads, etc, it is again time to replace the tyre. When replacing 
tyres, it is best advised to stick to the specifications as laid down by the car 
manufacturer. However, within the same size, you may replace your tyre with a 
greater load index and speed rated tyre.
