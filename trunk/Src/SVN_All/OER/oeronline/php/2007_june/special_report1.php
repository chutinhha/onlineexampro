</font><b><font face="Verdana" size="4">Women Power</font></b><font face="Verdana" size="1"><br>
<b>Women in Business Conference ’07 taking place in Muscat on June 2-3 will be a 
unique platform for the congregation of successful businesswomen and female 
corporate executives<br>
</b><br>
<img border="0" src="special.jpg" width="149" height="167" align="left" hspace="6" vspace="4">Today, more and more women are occupying top positions in the corporate world 
and business arenas. Indra Nooyi (PepsiCo), Anita Roddick (The Body Shop) and 
Raja Al Gurg (Eissa Al-Gurg Group), etc., are some of the names that are 
inspiring millions of women to come forward and take their share of pride in the 
business world. In recognition of this growing women power, a group of 300 of 
the world’s most powerful businesswomen are headed to South Africa in September 
this year for the Leading Women Entrepreneurs of the World Summit, which 
promotes and encourages female entrepreneurial excellence. The 300 women 
collectively own and run corporations that generate more than US$300 billion and 
employ 530,000 people in 56 countries. <br>
<br>
Not to be left behind, women in Oman are also making their presence felt in the 
business and corporate arenas. Many Omani women are spearheading businesses in 
services and industries, and a lot more are leading corporate entities to 
greater heights. In recognition of the success of women and to further promote 
such enterprise, the first-ever annual Women in Business Conference is scheduled 
to be held in Muscat on June 2-3. Organised under the patronage of HH Sayyida 
Aliya Bint Thuwaini Al Said (see interview: Courage of Conviction), the 
objective of the conference is to bring together successful businesswomen and 
female entrepreneurs to share their experiences and exchange ideas. <br>
<br>
Elisabet Sjostrand, managing partner, Envent, the organisers of the conference, 
says that “the conference will have about 250 women delegates, holding middle to 
high-level managerial positions in corporates in Oman besides business students 
from the university. Though the focus is on girl students and businesswomen, the 
conference will not bar men from attending.” The conference will include 
productive and interactive workshops, networking and the spirited launch of 
Women in Business Awards. <br>
<br>
<b>Rising to the fore</b><br>
Events of such nature in Oman are bound to have a wider impact, as women at 
large are yet to optimally contribute towards the economic development of the 
Sultanate. Women comprise 49.5 per cent of the Omani population but they don’t 
have an adequate representation in the private and government sector. As per the 
Ministry of National Economy’s Statistical Year Book published in October 2006, 
out of 87,891 Omanis in the civil service in 2005, only 32,566 were females. The 
private sector figures are more dismal; out of a total of 98,537 Omanis working 
in the private sector and registered with Public Authority for Social Insurance, 
only 16,625 were females. <br>
<br>
But future prospects for Omani women are brighter, as the younger lot is not 
only matching but excelling over young counterparts in getting the right kind of 
education. In 2005-06, girls comprised 49 per cent of the total students at the 
school level. Moving up, at the college level, 55 per cent of the students were 
girls. In technical colleges, the girls also came up with a good show, making up 
41 per cent of the students. Interestingly, it is in tourism, which is the focus 
of Omani government in its move to diversify the economy, that females are seen 
making a dominating presence. Thus, in Oman College for Tourism, 71 per cent of 
the students were girls while in the College of Banking &amp; Financial Studies, 42 
per cent of the students were girls. More Omani girls are studying abroad 
compared to their male counterparts. This all indicates that women are going to 
play a bigger role in Oman’s quest to become a diversified economy with an 
important position in the global market.<br>
