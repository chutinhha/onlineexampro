<p align="justify">
<b><font face="Verdana" size="4"><br>
</font></b><b><font face="Verdana" size="4">Building for the future</font></b><font face="Verdana" size="1"><br>
<b>Tourism in the Middle East is set to conquer new heights, with targeted 
investments building new attractions and promoting new destinations, says Jason 
J. Nash<br>
</b>
<img border="0" src="economy.jpg" width="147" height="427" align="left" vspace="4" hspace="6"><br>
The 
Middle East is on the verge of an investment spree in the tourism sector unlike 
anything the global industry has ever witnessed, with at least US$1 trillion set 
to be poured into hotels, resorts, attractions and associated infrastructure 
between now and 2020.<br>
<br>
According to an initial report released in early May, the region will see the 
capacity of its airports increased by an extra 300 million passengers, another 
200 hotels built adding 100,000 beds to the existing stocks and the numbers of 
visitors rising to 150 million. These figures do not take into account 
developments that have yet to make it to the planning stage, but are based on 
projects already announced.<br>
<br>
The 
report, prepared by consultancy firm Global Futures and Foresight (GFF) and 
presented at the Arab Travel Market (ATM), the region’s leading tourism expo, 
said that the countries of the Middle East have the ambition, resources and 
commitment to turn this vision into reality.<br>
<br>
The results of the GFF study were backed up by another study, conducted by the 
World Travel and Tourism Council, which predicted that tourism will claim about 
10 per cent of all Middle East investment annually through to 2016.<br>
<br>
<b>Destination ME<br>
</b>There are a multitude of reasons behind the boom in Middle Eastern tourism 
investments, including social, financial and political. Perhaps the foremost of 
these is the concerted drive by most states in the Gulf and beyond to prepare 
their economies for life after fossil fuels.</font></p>
<p align="justify">
<font face="Verdana" size="1">
Dubai and Abu Dhabi have led the way for economic diversification and 
liberalisation, with most of the other states in the region catching up fast. 
Great effort, and an even a greater amount of cash, has been expended to develop 
industries, foster private sector investment, attract foreign firms and to 
create an environment for doing business. </font></p>
<p align="justify">
<font face="Verdana" size="1">
<img border="0" src="economy02.jpg" width="200" height="117" align="left" hspace="6" vspace="4">However, nowhere is this more apparent than in the tourism sector. If 
successful, this campaign will pay off handsomely and help sustain the region’s 
economy in the future. Quite apart from the revenue that tourism will bring in, 
between now and 2020, some 1.5 million jobs will be created in the hospitality 
industry in the Middle East. While many will go to expatriate workers, new 
opportunities will be opened up for locals, one of the underlying motives for 
the massive shift to tourism.<br>
<br>
Another cause for the promotion of a home grown tourism industry is the sense of 
alienation felt by many in the Middle East in the wake of the terrorist attacks 
against the US in September 2001. While there was an across-the-board increase 
in travel security in the West, with visa requirements tightened and processing 
procedures at ports of entry stepped up, visitors from the Middle East have felt 
particularly targeted and unwelcome.<br>
<br>
<img border="0" src="economy03.jpg" hspace="0" style="border: 2px solid #EFEFE9; ">A very direct outcome of this was the ramping up of tourism investments in the 
region, seeking to provide facilities and attractions offered by more distant 
destinations without the feelings of suspicion and distance. It is perhaps 
telling that apart from a few firms that operate in the Middle East, about the 
only US representation at the recently concluded ATM was from the Office of 
Homeland Security. With major promoters having apparently given up on the Middle 
Eastern market, explanations of finger printing techniques were hardly likely to 
woo prospective tourists.<br>
<br>
The oversight could be a costly one. According to the World Tourism Organisation, 
tourists from the Gulf region spent more than US$12 billion on overseas holidays 
in 2005, the last year for which full figures were available. Much of this was 
spent in the more friendly and welcoming environment of the Middle East. <br>
<br>
However, while many in the Middle East may feel somewhat ostracised by the West, 
those feelings do not extend their culture. Recently signed deals will see 
Universal Studios, Marvel Comics and Nickelodeon Recreation build theme parks in 
Dubailand, the Emirate’s multi-billion dollar recreation and holiday centre. 
More upmarket are the agreements reached by Abu Dhabi to have two of the world’s 
premier art galleries, the Louvre and the Guggenheim, to lend their names and 
their works to new purpose built centres.<br>
<br>
Though there has been a rush to sign up some of the leading icons of 
international popular and high culture, it is the local colour of the region 
that is to be the main draw card for the Middle East’s tourism industry.<br>
<br>
<img border="0" src="economy04.jpg" hspace="8" style="border: 2px solid #EFEFE9; " align="right" vspace="4"><b>Exci</b><b>ting possibilities</b><br>
“The challenge now is to drive the next wave of innovation that emphasises, 
celebrates and promotes Middle Eastern cultures, heritage and tradition,” said 
David Smith, joint chief executive of GFF.<br>
<br>
There is also a shift in emphasis within the trade. Where once it was almost 
exclusively focused on the top end of the market, with seven star hotels and 
luxury resorts, there is a growing awareness of the vast and little tapped 
medium range tourism sector. <br>
<br>
This shift has spawned a number of budget airlines, such as Air Arabia and 
Jazeera Airways, and increased investment in more affordable hotels and 
accommodation. Where once the Middle East may have been looked upon as a 
playground for the wealthy, it is now becoming a playground for all. <br>
<br>
Though funds have been committed to develop the tourism sector, infrastructure 
put in place and the full gamut of activities catered to, the industry still has 
hurdles to overcome. One of these is duplication of efforts: there is active 
rivalry between some of the states in the region to build the biggest and the 
best.<br>
<br>
In part, at least, this is starting to be addressed, with a number of Gulf 
states identifying particular niche markets. As an example, Oman is actively 
promoting itself as an adventure holiday destination, its mountains and rugged 
terrain ideal for trekking, camping and safari tours. The Sultanate is also 
pushing its potential for medical tourism, using its hospitals and health 
facilities to draw in international visitors in need of specialised care.<br>
<br>
Other states have given particular attention to niche segments such as sports, 
with facilities for internationally popular leisure activities, such as golf, 
cricket, car racing and rallying and aquatic sports.</font></p>
		<p align="justify">
<font face="Verdana" size="1">
<b>Need perceptions<br>
</b>The well-developed transport infrastructure in the Gulf region too can play its 
part, allowing visitors to move between states quickly and easily, sampling the 
best each has to offer.<br>
<br>
Another sub-sector where business is becoming brisk is professional tourism: 
meetings, incentives, conferences and exhibitions, more popularly known as MICE. 
While it is again Dubai and Abu Dhabi that lead the way in catering to this 
increasingly lucrative form of tourism, states such as Bahrain and Kuwait are 
taking an increasing slice of the market.<br>
<br>
Another challenge that the GFF report identified was the “collective identity” 
that the region suffers from, again in part harking back to the perceptions 
generated by 9/11, a challenge, it said, that can be met through vigorous 
security measures.<br>
<br>
While money alone cannot guarantee success in any venture, there is a growing 
understanding that while it is all very well to draw tourists in with the lure 
of fine hotels, golden beaches and year round sun, which most of the countries 
in the region have in abundance, discerning visitors want more. This has meant 
that there is an increasing focus not just on investments but also on targeted 
investments, allowing the Middle East’s tourism industry to mature and broaden. 
It is now not so much a case of “if you build it, they will come” as “if you 
build what they want, they will come”. </font></p>

