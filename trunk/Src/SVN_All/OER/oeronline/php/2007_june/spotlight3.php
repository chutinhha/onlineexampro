<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Oil’s well that ends well</font></b><font face="Verdana" size="1"><br>
<b>Want to know what makes your car lubricant work properly? Here’s a step-by-step 
guide to a simple process that can save you money and is a lot easier than it 
sounds <br>
<br>
</b>
<br>
<img border="0" src="spot3.jpg" width="250" height="149" align="left" hspace="6" vspace="4">It is important to remember that when it comes to maintenance, safety always 
comes first. So whether you’re a first-timer or an old hand, follow this 
step-by-step tutorial to know the safest and most efficient way to perform the 
simple task of changing the lubricant of your car.<br>
<br>
<b>Choose your motor oil </b> <br>
If your schedule involves frequent short trips, stop-and-go traffic or towing 
and hauling, the extra strain on your engine will necessitate more frequent 
changes. Be certain to check the vehicle’s manual for special conditions and do 
not exceed warranty recommendations.<br>
&nbsp;<br>
<b>Prepare your vehicle <br>
</b>Always consult the vehicle’s manual for specific safety precautions before 
sliding under your vehicle. Never use a jack to hold your vehicle up – it’s 
simply too unstable. Portable wheel ramps are ideal and much safer. Wheel ramps 
will tilt the car just enough to allow you to slide underneath. Make sure you 
follow your wheel ramp manufacturer’s instructions carefully, especially 
concerning safety issues. After making sure that you are on level ground, drive 
your vehicle up onto the wheel ramps so that the front tyres are elevated. Set 
your handbrake, and brace both rear wheels with wooden blocks to prevent the 
vehicle from rolling. Put your vehicle in first gear if you have a manual 
transmission or in ‘Park’ if you have an automatic transmission. Cold oil will 
not drain properly, so before starting work, drive your car around to raise the 
oil temperature. Then switch off the engine and raise the bonnet to loosen the 
oil filler cap to avoid creating a vacuum. This will allow the oil to drain from 
the bottom more freely.<br>
<br>
<b>Drain the old oil <br>
</b>Locate the oil drain plug on the underside of your vehicle. It should be located 
at the bottom rear end of the engine sump or oil pan. Place the drain pan 
underneath the drain plug and slightly toward the back. Using your wrench, turn 
the plug counter-clockwise until it rotates freely. Finish removing the plug by 
hand. At this point, be careful of the oil since it may release rapidly and is 
likely to be rather hot. Try not to drop the plug into the pan, but don’t worry 
if you do! <br>
<br>
<b>Remove the oil filter <br>
</b>Loosen the oil filter by turning it counter-clockwise with a filter wrench. 
Complete the removal by hand, taking care not to touch the hot exhaust manifold. 
The oil filter may be filled with oil and feel slightly heavy, so carefully ease 
it down and away from the engine and tip its contents into the drain pan. <br>
<br>
Some oil filters are mounted horizontally and when you loosen them they will 
leak some dirty, hot engine oil. Don’t worry, this is normal, but be ready for 
the leak by having several absorbent rags. Take your rag and wipe in and around 
the filter seat on the engine.<br>
<br>
Then take a new filter and use your finger to apply a light film of oil (new or 
used) to the gasket (the circular edge of the filter itself), which acts as a 
sealant. Carefully screw the new filter onto the threaded oil line, turning it 
clockwise. Once aligned properly, the filter will thread on easily. Tighten the 
filter by hand, taking care not to over tighten. Clean the oil plug and copper 
gasket and then align and replace the plug. Screw the plug in by hand and finish 
by tightening it with a wrench. Once again, don’t over tighten.<br>
<br>
<b>Replace the oil filter</b><br>
Take your rag and wipe in and around the filter seat on the engine. Then take a 
new filter and use your finger to apply a light film of oil (new or used) to the 
gasket (the circular edge of the filter itself), which acts as a sealant. 
Carefully screw the new filter onto the threaded oil line, turning it clockwise. 
Once aligned properly, the filter will thread on easily. Tighten the filter by 
hand, taking care not to over tighten. Clean the oil plug and copper gasket and 
then align and replace the plug. Screw the plug in by hand and finish by 
tightening it with a wrench. Once again, don’t over tighten. <br>
<br>
<b>Add clean oil <br>
</b>Carefully release the handbrake and allow the car to slowly roll back down the 
ramp to level ground (do not run the engine at this stage or severe damage will 
occur). On the top of the engine you will find the Oil Filler Cap usually marked 
with the symbol of an oilcan. Unscrew the cap and proceed to fill the engine 
with the required quantity of oil, checking with the dipstick to ensure proper 
fill level. Replace the cap and wipe off any spillage. The oil light should go 
out as soon as the engine is started. Run the engine for several minutes, then 
switch it off and check the dipstick once again to ensure proper oil level. It 
is likely that you will have to add some more oil at this point. Last, but not 
the least, check under the vehicle for leaks – especially around the oil filter 
and sump drain plug. </font>
