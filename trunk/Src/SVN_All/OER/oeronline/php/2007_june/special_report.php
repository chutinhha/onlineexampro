<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The ONIC Chronicle</font></b><font face="Verdana" size="1"><br>
<b>The vision of two men may spark off a transformation in the currently dull 
Oman mutual fund scenario. ONIC Holding is scripting a new path in alliance with 
a Canadian investment management company to usher in the world to Oman 
investors. Ramesh Kumar chronicles the path-breaking venture by tracking the 
promoters</b><br>
<br>
<img border="0" src="special_report01.jpg" hspace="8" style="border: 2px solid #EFEFE9; " align="left" vspace="4" width="300" height="199">On 
the surface, there is nothing common between the Sultanate of Oman and Canada. 
They are worlds apart: politically, socially and economically. And, things would 
have remained that way but for one individual – Zaigham Shah from Pakistan.<br>
<br>
When Zaigham Shah first set foot on the Sultanate soil a couple of years ago, he 
could not have visualised the prospect of being the architect of the first-ever 
joint venture between an Omani corporate entity and a Canadian one. He neither 
knew the affable Akbar Habib, the Group Chief Executive Officer of ONIC Holding, 
nor did he know of the existence of Asif Khan, an Indian entrepreneur settled in 
Toronto, running FrontierAlt. It was sheer coincidence that brought Zaigham into 
contact with both these men. It was a time when both Habib and Asif Khan were 
looking at the world map, seeking to explore new territories beyond their 
respective zones: Akbar beyond Oman and Asif beyond Canada. It was August 2006 
and Shah had left Oman to join Asif in Toronto. His stay in Oman had put him in 
touch with Akbar’s ambition, his drive, and his vision. And once in Toronto, he 
soon realised that Asif was a carbon copy of the ONIC Holding honcho. Their 
‘shared vision’ unknown to each other, spotted by Zaigham, resulted in a meeting 
between them. The net result: the first-ever Omani-Canadian joint venture of one 
listed and one private company.</font><p align="justify" style="margin: 3px 10px">
		<font face="Verdana" size="1"><b><br>
		Why Middle East?<br>
		</b>“The Middle East was part of our growth plans long before we met the 
		team from ONIC. A new world financial centre is developing in the region 
		that rivals Europe and the US. Also, there is a large market of 
		investors that are only now beginning to be offered products that meet 
		their needs and preferences. We see this as an important opportunity,” 
		reasons the bespectacled mid-30s FrontierAlt chief.<br>
		<br>
		The Middle East market is very important to FrontierAlt. In addition to 
		participating in the excellent growth of the region, FrontierAlt wants 
		to be at the heart of the emerging, but booming, Islamic investment 
		industry.<br>
		<br>
		For ONIC Holding, the motivation was different. The 2001-03 phase was a 
		tough period for the Omani company. It was in the doldrums. Survival was 
		the focus area. Akbar admits that flagship assets such as the Commercial 
		Bank of Oman, Al Ahlia Securities, Warranty Holding had been sold. The 
		enormity of the situation can be gauged from the fact that ONIC 
		Holding’s consolidated total assets came down from US$196.3 million in 
		March 2001 to US$107.5 million in March 2003. In the process, the 
		company also reduced its debt burden – by almost two-thirds – from 
		US$77.9 million (March 2001) to US$25.9 million (March 2002).<br>
		<br>
		Once the firefighting was complete, Akbar began looking at a fresh 
		canvas. Stability was next on his agenda and over the next two years 
		(April 2003-March 2005) he restructured the group by turning around and 
		selling OIFC and building value in Al Ahlia Insurance, Orix Leasing and 
		National Life Insurance. Net result: total assets ballooned to US$202 
		million by March 2005, with net profit increasing to US$33.2 million.<br>
		<br>
		In April 2005, a resurgent ONIC Holding went into a strategic huddle to 
		pencil a scorching growth plan. “As is evident, we have been pursuing a 
		planned path to achieve the company’s vision of building assets to 
		secure sustainable income. We anticipate that by 2010 more than 50 per 
		cent of our sustainable income would be generated from outside Oman and 
		we will have an asset management company with a global footprint, 
		associates and partners. It is part of this global aspiration that made 
		us look beyond Oman,” elaborates Akbar, seated in his spacious Ruwi 
		corporate headquarters, hardly a stone’s throwaway from the Ministry of 
		Commerce &amp; Industry.<br>
		<br>
		Once the Look Beyond Oman exercise began in April 2005, Akbar and his 
		team began scouting various emerging markets and developed countries. 
		“Canada – a G7 country with a strong financial services market attracted 
		our attention,” says Akbar. “And in FrontierAlt, we found a perfect 
		fit.”<br>
		<br>
		It was not that the Canadian company was solely banking on ONIC for its 
		Middle East entry. It had held wide-ranging dialogue with several other 
		groups but, luckily or unluckily, none represented the same level of fit 
		that ONIC did. What interested Asif the most was ONIC’s team. “They’re a 
		smart, ethical group with a vision and outlook similar to ours. Also, 
		ONIC’s partners and investors represent some of the very best companies 
		in the Middle East region. There are synergies at all levels. In 
		addition, ONIC is beginning its international growth process and we 
		believe that there is a good case for FrontierAlt to grow in step with 
		ONIC,” explains Asif.<br>
		<br>
		FrontierAlt plans to leverage the ONIC network for exposure in the 
		Middle East, specifically Oman. The FrontierAlt Oasis group of Shariah 
		funds are a particularly good fit and the joint venture will endeavour 
		to make the ME community aware of their benefits.<br>
		<br>
		What does the FA-ONIC alliance mean to Oman? FrontierAlt currently works 
		with some of the very best money managers in Canada – from small teams 
		of two or three award-winning managers, to Canada’s largest company by 
		market cap. This will be used as a resource to support local investment 
		management in Oman. FrontierAlt, together with ONIC, offers a North 
		American gateway for Omani businesses and investors. <br>
		<br>
		For growth-focused Akbar, the choice of working with FrontierAlt was 
		irresistible. Elaborates the ONIC chief: “The profile of FrontierAlt 
		perfectly fits our vision and strategy. Considering the strong platform 
		the company has built, FrontierAlt is poised for exponential growth 
		within and outside Canada. It is attractively valued for a company that 
		is about to take off. The company represents a mix of young, dynamic and 
		talented professionals, with a very impressive track record. The company 
		is geared to launch new products like the Global Dividend Fund, Shariah 
		Compliant Funds, a Global IPO Fund, a GCC dedicated fund, etc. The 
		strength of FrontierAlt in the field of AMC and deal sourcing will 
		benefit ONIC in its future investment strategy. FrontierAlt will provide 
		access to ONIC to some of the best global fund managers and fund 
		management companies. Joining hands with FrontierAlt will help our 
		planned asset management company get a global footprint.”<br>
		<br>
		Agrees Asif, “the main option that we considered was to continue our 
		current business plan on our own and take investment from our existing 
		shareholders. ONIC has chosen to invest in FrontierAlt at just the right 
		time – we are breaking out in Canada and the company is at an inflection 
		point. FrontierAlt is seeing unprecedented support from the largest 
		players in the Canadian industry. We are poised for rapid growth and 
		gross profits are growing.”<br>
		<br>
		“Expected revenues would have continued to support the growth of 
		FrontierAlt, but the deal with ONIC has resulted in an alliance that 
		allows for expansion into the GCC at a much faster pace and yields the 
		opportunity to capitalise on the tremendous industry support we are 
		experiencing.”<br>
		“Also, most other potential investors overlapped our business and didn’t 
		offer the international reach and matching strategic outlook that ONIC 
		does.”</font><p align="justify" style="margin: 3px 10px">
		<font face="Verdana" size="1"><b><br>
		Synergy<br>
		</b>FrontierAlt will provide a good launching pad for ONIC to reach out 
		into the North American financial markets. Canada is a growing economy 
		and the only G7 country running a surplus for eight years running, with 
		a rich natural resource base. Through FrontierAlt, ONIC will be 
		participating in and sharing the growth of the country. Entry into the 
		North American market, with immense opportunities for acquiring 
		undervalued and turnaround companies, will help ONIC to diversify from 
		the GCC region. Through this tie-up, we will be creating a bridge 
		between two major economies. FrontierAlt will be the face for ONIC AMC 
		in North America. ONIC AMC would be the face for the regional investors 
		in the North American market. Cross selling of products and expertise 
		will be possible.<br>
		<br>
		FrontierAlt has a long-standing relationship with several insurance 
		companies, energy and mining companies, which could potentially support 
		growth of their respective core business. This partnership will provide 
		ONIC diversification in its asset mix and source of sustainable income. 
		The acquisition will positively support the growth aspirations of ONIC 
		and will be a step in the direction of becoming a financial services 
		conglomerate with a global outlook and reach.</font><p align="justify" style="margin: 3px 10px">
		<font face="Verdana" size="1"><b><br>
		New Entity<br>
		</b>Considering the strong brand and platform created, the name 
		continues to be FrontierAlt Investment Management Corp. However, both 
		sides are reluctant to divulge more financial details. Says Akbar: “ONIC 
		has always placed significant importance on the qualitative aspects 
		rather than the transaction amount. This is because this is just the 
		beginning of the quantum growth expected in FrontierAlt that will 
		require additional capital.” Nonetheless, he hastens to add that ONIC 
		Holding along with Asif Khan are the largest shareholders of 
		FrontierAlt.</font><p align="justify" style="margin: 3px 10px">
		<font face="Verdana" size="1"><b><br>
		Business Plan</b><br>
		“Our business model,” avers Akbar, “is to be a global investment bank 
		that provides a wide range of financial solutions across the globe. It 
		is with this global vision that we are incorporating an asset management 
		company and have acquired 20 per cent stake in FrontierAlt. Through this 
		structure, we will be creating a bridge between GCC and North America.”<br>
		<br>
		ONIC Holding believes in having independent set-ups for its subsidiaries 
		and associates with common and synergistic policies, procedures and 
		philosophies. Consequently, each company will have its own head, with 
		Akbar Habib being the Group CEO. Chairman and CEO of FrontierAlt will 
		continue to be Asif Khan and the company will operate from North 
		America. </font>
		<p align="justify" style="margin: 3px 10px">
		<font face="Verdana" size="1"><b><br>
		Official Launch</b><br>
		As part of corporate governance, the acquisition has been made public to 
		regulators. Product launch will materialise after the AMC is 
		incorporated and will include financial solutions for corporations, 
		financial institutions, governments and high net-worth individuals. 
		Other services include portfolio management, investment advice, and 
		corporate finance. ONIC Holding has already obtained the regulatory 
		approval from the Capital Market Authority (CMA) for setting up the AMC. 
		With a view to synergise the activities between the two companies, 
		FrontierAlt will also participate in some percentage ownership in the 
		AMC. FrontierAlt’s structured products, exclusive limited-partnership 
		deals, etc., in Canada will be also offered for subscription to select 
		clients in the region.<br>
		<br>
		It is no secret that the mutual fund scenario in Oman is dull and 
		lacklustre. Will the ONIC-FrontierAlt marriage change all that? Akbar 
		and Asif are optimistic on that front. “We are building an organisation 
		that will have a global reach – in North America through FrontierAlt, in 
		India through our FII, in Pakistan through Pak Oman AMC (our associate), 
		etc. Consequently, we shall not be restricted to the domestic market. 
		Also, through this platform we shall be in a position to offer global 
		financial solutions to our domestic customers. We believe that with the 
		above stated business model, our global reach, good and dynamic team 
		across all our organisation, excellent network, and support of our board 
		and regulators, we shall be able to provide totally new products and 
		financial solutions that will in due course become an integral part of 
		the investment culture.”</font><p align="justify" style="margin: 3px 10px">
