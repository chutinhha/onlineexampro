<p align="justify" style="margin-left:10px; margin-right:10px; margin-top:3px; margin-bottom:11px" dir="ltr"><font face="Verdana" size="1"><br>
</font><font size="4" face="Verdana"><b>The Peggy-Karl Saga</b></font><font face="Verdana" size="1"><br>
<b>When good ideas are backed by finance, success, they say, is round the 
corner. The corner in question is very much in Oman, at the Salalah Free Zone. 
OER quizzes the investor-promoters</b><br>
<br>
<img border="0" src="passing_by.jpg" width="300" height="241" align="left" hspace="6" vspace="4">At 
first appearance, they resemble a couple on a holiday. Picture perfect they are, 
as the accompanying visual reflects. The truth is they are not. They are, 
however, married to a common idea viz., making money, or rather, to creating 
wealth for themselves as well as others.<br>
<br>
As Peggy Farley, Chief Executive Officer (Public Capital Markets) of the New 
York-based Ascent Group, puts it: “Before December this year, you will see us 
investing US$3 million in Oman”. Farley watches you closely before throwing this 
one: “It is just the beginning. We have already collected US$30 million and by 
June end, our kitty will be roughly US$100 million. All this money is meant for 
investing in the Middle East – particularly Jordan and Oman.”<br>
<br>
She is not joking. She is a seasoned financial wizard. In the 1970s, she worked 
with the Citibank in the Middle East. Returning to New York, she was roped in by 
Morgan Stanley, where she had spent close to six years counseling them on the 
Middle East ground realities. Around 1988, she met Karl Groth, Chief Executive 
Officer (Private Capital Markets), an alumni with a science background from the 
University of Minnesota. That is when Ms Wizard met Mr Ideas and decided to 
partner a new venture.<br>
<br>
Why Oman? The Free Trade Agreement (FTA) with the United States, pat comes the 
response from Groth. The Peggy-Karl duo has already identified an Omani joint 
venture partner to set up shop in the Salalah Free Zone. Ascent Group and 
Salalah Medical Supplies Manufacturing Company have filed an application with 
the Ministry of Commerce and Industry to float a new corporate entity – Salalah 
Medical Device Manufacturing Co – with 40 per cent equity each, while the 
balance 20 per cent is reserved for employees.<br>
<br>
According to Groth, their search for an Omani partner began earnestly in 2003 
and Salalah Medical Supplies Manufacturing Company’s track record was 
stupendous. Farley goes to the extent of giving it a 9 out 10 rating in terms of 
global practices. Above all, what convinced the private equity fund or venture 
capitalists is the European Union approval for the Salalah company’s processes 
conducted every six months. “We did our own due diligence and have no 
complaints,” adds Groth. As and when the new facility goes upstream in sometime 
2008, it will generate 200 jobs for Omanis.</font><p align="justify" style="margin-left:10px; margin-right:10px; margin-top:11px; margin-bottom:3px" dir="ltr">
<font face="Verdana" size="1">
If all goes well, the equipment for the new Salalah plant ought to be in place 
by September. After recruitment and rigorous training, the plant should go on 
stream in early 2008. Obviously, there will be a new management team in place, 
representing the requisite skill sets – both managerially and technically. The 
European Union, GCC and the US will be target markets for the Salalah-produced 
products.<br>
<br>
The initial investment of US$3 million is okay. But it is a niggardly sum 
compared to the huge corpus of US$100 million they will have soon. “We are 
constantly on the look out for new projects and new partners in this region. 
Jordan and Oman are prime target areas for us, thanks to the FTA which is 
already in place,” elaborates Farley. Significantly, the Ascent Group is 
preparing a feasibility report to manufacture ‘D-ribose’ – a cardiovascular 
medical device – in the Salalah Free Zone again. It will be a greenfield project 
and one of the focus areas for the new investors is to assess the local ability 
to fabricate and build a world class manufacturing facility from scratch. “We 
are confident Omanis are capable. If that happens, it will slash down the cost 
drastically,” says the 60- plus Farley. Believe it or not, Oman has arrived on 
the global radar, asserts Groth. World class products at relatively low cost to 
feed the huge market for such devices from Omani soil. Isn’t it enticing?<br>
