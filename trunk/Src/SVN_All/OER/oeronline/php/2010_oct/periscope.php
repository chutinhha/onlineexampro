					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">OPEC and crude 
					oil market<br>
					</font><font face="Verdana" size="2"><br>
					OPEC will have to accommodate the strategic interests of 
					non-member states in its decisions and engage in strategic 
					diplomacy with the rising industrial powers in Asia</font></b></p>
					<p align="center" style="margin: 3px 10px"> 
				&nbsp;</p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">OPEC was founded in September 
					1960 when the chairman of Exxon, eager to protect his 
					corporate market share in Europe from cheap Soviet Russian 
					imports, unilaterally cut the posted price of oil. Exxon’s 
					price cut enraged the world’s oil producing states, most of 
					whom were dependent on the export of black gold for their 
					survival.<br>
					<br>
					However, OPEC’s bargaining power was miniscule since the 
					logistics of the global oil and gas industry was entirely 
					dominated by the fabled Seven Sisters, integrated 
					supermajors who controlled every facet of the business from 
					upstream production/ exploration to downstream 
					refining/marketing in a world awash with oil. Yet global 
					inflation and geopolitical supply shocks (the 1973 Middle 
					East war, the closure of the Suez Canal, the coups in Libya 
					and Iraq and the Iranian revolution) swung the balance of 
					power in oil pricing in favour of OPEC in the 1970s, with 
					Saudi Arabia emerging as the world’s leading oil exporter 
					and the central bank of oil, with its 10mn daily production 
					and ownership of one fourth of the planet’s oil reserves.<br>
					<br>
					</font><b><font face="Verdana" size="2" color="#000080">
					Co-ordinated production cut</font></b><font face="Verdana" size="1"><br>
					<br>
					The world oil market faced systemic trauma after the failure 
					of Lehman Brothers, the subprime carnage on Wall Street and 
					the onset of global recession. Crude oil prices plunged from 
					$148 to $32 in six months. A freefall in oil prices would 
					have proved catastrophic for the economies and government 
					budgets of the world’s oil exporters, including all six GCC 
					states. So OPEC united under the leadership of its “swing 
					producer” Saudi Arabia and orchestrated the largest 
					production cuts in its history, cutting more than 4mn 
					barrels a day in production. This was the primary reason, 
					coupled with the global economic recovery in the US and the 
					trillion dollar bank lending spree in China, that crude oil 
					prices snapped back from $35 in December 2008 to the $70-80 
					range now.<br>
					<br>
					OPEC had learnt the lesson of the 1999 oil price free fall 
					all too well, when the Asian financing Armageddon led to a 
					savage downturn in oil demand but internal political 
					divisions within OPEC prevented a coordinated production 
					cut. The oil producers suffered terribly when spot oil 
					prices fell below $10. The pain of $10 oil even forced 
					mergers among Seven Sister companies Exxon and Mobil, 
					Chevron and Texaco and persuaded BP’s Lord Browne to bet 
					Britain’s oil colossus on risky deepwater drilling in the 
					Gulf of Mexico and the political tempests of Putin’s Russia. 
					The ghosts of 1999 ensured that Saudi Arabia was able to 
					orchestrate a production cut on a scale that was unthinkable 
					a decade ago.<br>
					</font><b><font face="Verdana" size="2" color="#000080"><br>
					Strategic diplomacy</font></b><font face="Verdana" size="1"><br>
					<br>
					Yet OPEC cannot rest on its laurels even though it has 
					reestablished its central role in the production and pricing 
					of crude oil. New major oil discoveries in Brazil and West 
					Africa mean that OPEC will have to accommodate the strategic 
					interests of non-member states in its decisions, as it 
					accommodated the interest of Britain and Norway after the 
					North Sea strikes in the 1970s, Mexico after the Gulf of 
					Campeche discoveries in the 1980s and Russia after the 
					collapse of the USSR in August 1991. Moreover, Iraq intends 
					to spend hundreds of billion of dollars to quintuple oil 
					production to ten million barrels a day in the next decade, 
					raising open the possibility of a future oil glut. 
					Government subsidies in the Gulf have also encouraged 
					massive domestic consumption of gasoline, electricity and 
					air-conditioning, a scenario that will influence export 
					policies.<br>
					<br>
					OPEC will have to engage in strategic diplomacy with the 
					rising industrial powers in Asia, primarily China and India, 
					to recalibrate its traditional economic and political ties 
					with the US, Britain, Germany and France. It is no 
					coincidence that Saudi Arabia’s King Abdullah made his first 
					state visit after succeeding to the throne to Beijing, the 
					kingdom’s new giant consumer whose factories and highways 
					will determine the future of the oil and gas business. The 
					Politburo in the Middle Kingdom is obsessed with energy 
					security on a scale that is not the case with the US, which 
					has a huge domestic oil producing industry in Texas, 
					Oklahoma, Louisiana and the Rocky Mountain states. China and 
					India, despite their awesome industrial power and export 
					prowess, still have desperately poor citizens, more than a 
					billion peasants who cannot afford food price inflation 
					spirals caused by a spike in oil prices. <br>
					<br>
					OPEC’s engagement with China and India could well result in 
					more stable, long term oil deals, with the result that oil 
					prices could trade in a range acceptable to both consumers 
					and producers. The Saudis, for instance, have repeatedly 
					stressed that their optimal price point for crude oil is 
					$75-80 a barrel. If the West, China and India are obsessed 
					with oil supply shocks, OPEC’s oil ministers in its Vienna 
					conclave are obsessed with oil demand shocks. It is no 
					secret that Saudi Aramco, the world’s largest oil company, 
					has invested more than $20bn in Asian refineries, the 
					biggest buyers of Gulf heavy sour crudes shipments. The 
					world economy’s reliance on OPEC oil will only grow in the 
					decade ahead.<br>
					<br>
					OPEC, in turn, will need to manage the divergent interests 
					of its members and consumers as well as accommodate the 
					production spike in Iraq in its current quota system. OPEC 
					member states, as Abu Dhabi’s Masdar City initiative 
					demonstrates, will also need to invest in the emerging 
					energy economy of green technologies like wind/solar and 
					biofuels. International politics could well complicate 
					OPEC’s internal equilibrium. However, OPEC survived the 
					Iran-Iraq war two decades ago and its member states have 
					surely learnt that cooperation, not confrontation, is 
					mission critical in the quest for stable oil pricing.<br>
