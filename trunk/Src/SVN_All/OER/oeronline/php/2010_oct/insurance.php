		        <td valign="top" width="100%"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">a qualitative 
					change</font></b><br>
					<font face="Verdana" size="2"><b><br>
					</b>
					</font><font face="Verdana" size="1"><br>
					<img border="0" src="images/oct_insurance.jpg" width="150" height="173" align="left">Gargash and Trade Links Insurance Services has emerged as a 
					one-stop-shop for insurance products in the Sultanate. The 
					company also plans to establish a pan-Oman presence in the 
					near future, writes <b>A Hari Kumar</b><br>
					<br>
					Around two months after launching operations, Gargash and 
					Trade Links Insurance Services (GTLS) which offers 
					professional insurance intermediary services, is all set to 
					bring a qualitative change to the insurance sector in Oman. 
					The company has emerged as the one-stop-shop for corporate 
					and individual customers who want to get professional advice 
					on insurance matters including risk evaluation and 
					management. Gargash Insurance is accredited as a chartered 
					insurance broker by Chartered Insurance Institute (CII) 
					London.<br>
					<br>
					Dr J Retnakumar, CEO, GTLS, says the response from insurance 
					companies, corporate, SMEs and individual customers has been 
					very encouraging. <br>
					<br>
					“Gargash is one of the most respected names in the insurance 
					industry and everybody is aware of our strengths and 
					expertise in the field. We expect to make a mark in the 
					insurance sector in Oman soon,” he says. The company has a 
					full fledged team providing excellent broking services to 
					all types of customers, without any cost to the customer. It 
					aims to reach all parts of Oman and plans to open offices in 
					Sohar, Salalah and Sur in the coming days.<br>
					<br>
					Commenting on his change from country manager of a leading 
					multinational insurance firm in Oman to CEO of a 
					professional broking firm, Retnakumar says he viewed it as 
					an opportunity to serve customers in a better way. “This is 
					a very interesting assignment which is at the same time 
					challenging too,” he adds.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Brokers’ relevance</b></font><font face="Verdana" size="1"><br>
					<br>
					“As a professional broking firm we are able to provide 
					competitive quotes from all leading insurers to customers. 
					We also help them select the policy best suited to cover 
					their risks. We help customers select the best coverage 
					based on competitive and financing terms and liquidity,” 
					says Retnakumar. GTLS has qualified professionals and a 
					customer friendly insurance software to service customers.<br>
					<br>
					All these services go to its customers, free of cost. 
					Moreover, its Oman operations are fully supported by Gargash 
					Insurance based in Dubai which has allied with Trade Links 
					Insurance Services to form GTLS.<br>
					<br>
					GTLS has signed agreements with several major insurance 
					companies in Oman and already has major corporate clients. 
					Pointing out the relevance of a professional insurance 
					broker, Retnakumar says, many clients are not fully 
					conversant with all the clauses of agreements which 
					insurance companies execute with them. Clients often do not 
					study the agreement fully and companies sometimes fail to 
					educate them. This might eventually lead to a strain in 
					relationships between both at the time of claims.<br>
					<br>
					The role of professional broker is to bridge the gap between 
					the two. They ensure that clients are adequately insured for 
					risks. Customers should not only consider cost of coverage 
					while selecting a policy but also the attitude of the 
					insurance company while making claims, notes Retnakumar. 
					They should select policies of those companies which protect 
					their interests adequately during the time of a claim even 
					if the cost of coverage is a little high, he observes. A 
					professional intermediary could assess all these for the 
					customer.<br>
					<br>
					Retnakumar observes that awareness of the public on 
					insurance is minimal in the Sultanate. “We plan to organise 
					a series of workshops and informal chats on different topics 
					in the insurance sector in the future with the support of 
					social organisations. We will organise safe driving 
					campaigns and awareness campaigns through print media and FM 
					channels,” he says. Creating awareness among the public on 
					insurance sector and customer delight would be our focus 
					areas in the coming days, he adds.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Future prospects</b></font><font face="Verdana" size="1"><br>
					<br>
					Oman offers excellent conditions for the growth of the 
					insurance industry. The Sultanate has one of the best 
					regulatory set ups in the world and the regulatory measures 
					implemented by Capital Market Authority (CMA) under the 
					leadership of HE Yahya bin Said Abdullah Al Jabri, its 
					executive president have benefited the insurance industry, 
					general public and intermediaries and are helping the steady 
					growth of the industry, says Retnakumar.<br>
					<br>
					Currently the insurance sector in the Sultanate is 
					experiencing slow growth but future prospects are bright. 
					The Sultanate is executing a slew of major infrastructure 
					projects and other engineering and housing projects which 
					offers great scope for the insurance sector. The economy of 
					the country is also growing steadily which will eventually 
					help the insurance industry.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Medical insurance </b></font><font face="Verdana" size="1">
					<br>
					<br>
					Referring to medical insurance segment, Retnakumar says it 
					has huge potential for growth. “In association with 
					insurance companies, we plan to design innovative and 
					tailormade health policies for various corporates as per 
					their requirement. But the use of medical insurance policies 
					needs to be monitored closely by individuals, insurance 
					companies, hospitals and clinics,” he says. <br>
					<br>
					Medical policies are successful as long as individuals and 
					companies find it viable. As cost of treatment is 
					escalating, there is a need to use medical insurance 
					policies judiciously. The practice of making patients 
					undergo unwanted costly tests should be curbed, otherwise 
					companies may wind up such policies, because of 
					non-viability, says Retnakumar.<br>
					<br>
					Commenting on the impact of recurring adverse weather 
					conditions in Oman on the insurance sector, Retnakumar says 
					many insurance companies and reinsurers have suffered from 
					the disbursal of huge amounts as claims following weather 
					disturbances. Flood and tempest coverage have now become a 
					major area of concern for insurance companies. Precautions 
					need to be taken while undertaking construction at 
					localities which experience frequent weather disturbances. 
					Meanwhile, those who take policies to cover risks arising 
					from weather disturbances should exercise caution while 
					selecting one,” he adds.<br>
					<br>
					In the light of all these, role of intermediaries in the 
					insurance sector in Oman has become very vital at the time 
					of executing contracts and making claims, observes 
					Retnakumar. There is big scope for professional insurance 
					intermediaries in the coming days, he adds<br>
