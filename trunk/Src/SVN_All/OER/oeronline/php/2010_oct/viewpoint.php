                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    Time for fundamental change</font></b><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2"><br>
				Responding to the recession, individual consumer products 
				companies have taken specific actions which have collectively 
				contributed to a fundamental change in their culture and 
				behaviour<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				The It is well recognised that the consumer products (CP) 
				industry has fared better than many other sectors during the 
				global economic slump. Most strikingly, the consumer landscape 
				has changed irrevocably, at least in mature markets. Value is 
				now in the forefront of almost all consumers’ minds. The 
				recession has also heralded a further shift in the relationship 
				between manufacturer and retailer, with the balance of power 
				tipping even further in favour of the latter.<br>
				<br>
				The recession has also marked the start of a significant shift 
				in the culture and behaviour of many consumer product companies. 
				Most have used the recession to become sharper, leaner and 
				fitter, accelerating pre-recession efficiency and simplification 
				programmes.<br>
				<br>
				</font><font face="Verdana"><b><font color="#000080">Reassessing 
				inventory<br>
				</font></b>
				</font><font face="Verdana" size="1"><br>
				Inventory reduction, in which progress has been more modest, 
				still offers a major opportunity. Retailers have already driven 
				some of these reductions, as they destocked in the early days of 
				recession; but many of them also have aggressive long-term 
				inventory-reduction targets.<br>
				While reducing inventory will drive efficiencies for the 
				retailer, it will also give brand owners more visibility. And 
				the ability to reduce inventories could help CP companies to 
				differentiate their overall offer to retailers, gaining vital 
				competitive edge in the process.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>A clear view of 
				risk</b></font><font face="Verdana" size="1"><br>
				<br>
				Risk management is a key challenge across the CP sector, 
				exacerbated by the global downturn. While the level and 
				management of risk vary enormously according to the region, many 
				companies recognise that though risk management and compliance 
				have not been core competencies, but they need to be. But 
				perhaps more important is that CP companies are also recognising 
				that they need to be smarter about mitigating risk while keeping 
				compliance functions streamlined and focused on the total cost 
				of controls.<br>
				<br>
				The potential failure of key suppliers has been a constant 
				threat during the recession. To reduce this risk, CP companies 
				have worked to gain full visibility of the extended supply chain 
				and either broaden or reduce their supplier bases.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>transparency</b></font><font face="Verdana" size="1"><br>
				<br>
				While all forms of business investment have come under increased 
				scrutiny, selective investment in IT has accelerated principally 
				because existing systems and infrastructures have been 
				inadequate to fulfill the needs of today’s global enterprises. 
				Although skepticism about the benefits of major IT programmes 
				still exists in many companies (not least due to the significant 
				cost), there is increasing acknowledgement that technology can 
				fundamentally improve visibility across the organisation.<br>
				<br>
				Companies continue to simplify and eliminate duplication, but it 
				now seems that cost-reduction programmes are increasingly moving 
				into a strategic and sustainable phase, more closely aligned 
				with changes made to global operating models.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>right price</b></font><font face="Verdana" size="1"><br>
				<br>
				Pressures on pricing and the management of the customer 
				relationship have understandably increased during the recession, 
				exacerbating the challenge of managing the price versus volume 
				equation. Commodity prices have been highly volatile and 
				continue to cause massive uncertainty.<br>
				While CP companies might not have been hit as hard by the 
				recession as most other sectors, they have nonetheless been 
				using it to get sharper, leaner and fitter, and as impetus to 
				challenge long-established preconceptions. In many ways, the 
				tough economic climate has driven a far more rigorous culture 
				and has provided greater license to make bolder decisions across 
				the organisation.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>gain market share</b></font><font face="Verdana" size="1"><br>
				<br>
				The worst pressure on CP companies should dissipate when the 
				recession ends, but the global slump will leave a lasting change 
				– an irrevocable shift in the consumer landscape. Consumers will 
				always expect value, but as consumer confidence returns they 
				will also be increasingly capricious, switching between value 
				and premium purchases. Brand portfolios will have to reflect 
				this dynamic. Categories in many sectors will also continue to 
				converge, such as health and wellness in food and beverages or 
				health and beauty.<br>
				<br>
				CP companies will also be looking to intensify an already 
				extreme focus on understanding consumer purchase patterns and 
				habits, in order to develop the right brands for tomorrow. Hard 
				discounters have made significant marketshare gains during the 
				recession, and these are likely to continue in the future.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>Revitalise 
				operations</b></font><font face="Verdana" size="1"><br>
				<br>
				The severity of the recession has forced many CP companies to 
				revitalise their existing operating models, as they seek to 
				develop competitive cost structures for the long term and a 
				platform for sustainable growth.<br>
				<br>
				Many CP companies still operate a country model, which 
				invariably results in widespread duplication and inefficiency. 
				Companies also recognise that talent could be deployed far more 
				cost effectively around the world, in a way that could reinforce 
				the global footprint and utilise IT more effectively to enable 
				better decision-making. How to carry out these activities – at 
				regional or country level and shared or outsourced service – is 
				equally difficult. But it does not stop there – executing a new 
				organisational structure is typically where companies leak 
				intended benefit, and sustaining the positives of a new 
				operating model in the long term is a further hazard.<br>
				<br>
				</font><font color="#000080" face="Verdana"><b>The long road to 
				change</b></font><font face="Verdana" size="1"><br>
				<br>
				Yet much work remains. For instance, for many companies, the 
				process of shifting to a more agile global operating model still 
				has a long road ahead. Opportunities in new emerging markets 
				need to be capitalised upon, brand portfolios require further 
				reshaping, supply chain forecasting needs to be improved, 
				inventories can be reduced significantly and sales force 
				execution needs to be sharpened.<br>
				Perhaps the most striking response to the recession is not the 
				specific actions of individual business units but that 
				collectively they amount to the start of fundamental change in 
				culture and behaviour. As the focus switches to preparing for 
				the upturn in global consumption, this appetite and capacity for 
				change must not end with the recession.<br>
