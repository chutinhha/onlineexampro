                    <img border="0" src="images/oct_environment.jpg" style="border: 2px solid #C0C0C0" align="right" hspace="9" vspace="4" width="200" height="235"></font><b><font face="Verdana" size="2">TAC 
				Eco Drive focuses on collective belief and action to reduce the 
				impact of irresponsible lifestyle on the environment. An OER 
				report</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">&nbsp;<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				is Eco Drive is an environmental consciousness and 
				respon-sibility awareness programme initiated by Towell Auto 
				Centre (TAC) to mitigate the challenges faced by environment due 
				to irresponsible lifestyle. As one of the first environment 
				campaigns from Oman, Eco Drive aims to motivate people to take 
				simple pragmatic steps to achieve the desired response on a 
				continued basis. It seeks to generate consciousness and 
				sensitivity towards the environment, especially among its staff 
				and customers, by promoting to do small things in daily life at 
				schools, homes and offices etc, which can go a long way to 
				create a difference for the community at large. <br>
				<br>
				As part of the campaign, green plants were sent with Eco Drive 
				messages to several corporates, decision makers, media people 
				and academia to raise awareness about environment. This inspired 
				other organisations too to take up the cause of environment 
				consciousness and conservation.<br>
				<br>
				</font>
				<font face="Verdana" size="2" color="#000080">
				<b>Effective strategies</b></font><font face="Verdana" size="1"><br>
				<br>
				TAC Eco Drive made a strategy to target people at two levels: 
				Internally it aimed at the staff and their families and 
				externally children in various schools across Oman. A multimedia 
				campaign was launched across daily newspapers and magazines in 
				addition to using radio as a medium to make the message reach 
				the masses. Besides, TAC produced and distributed bookmarks, 
				time tables, book stickers, folders, flags, etc to school 
				children and through the showrooms across Oman. It also 
				distributed green plants to major corporates and media in Oman 
				to remind the importance of environment. In order to educate the 
				customers, tips were given through POSM across TAC showrooms on 
				how to save fuel and maintenance costs.<br>
				<br>
				The purpose of the message was conveyed through interactive 
				presentations to all staff internally and to school children at 
				Indian school Wadi Kabir. The message of the campaign was also 
				disseminated through sponsoring the recyclable shopping bags for 
				ESO’s anti plastic bags campaign with TAC volunteers working in 
				various supermarket outlets to promote the campaign. <br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Looking 
				for evolution</b></font><font face="Verdana" size="1"><br>
				<br>
				The Eco Drive was launched as an integral part of the CSR agenda 
				at TAC and was incorporated as part of the company’s corporate 
				culture. The project is not intended to become a mission to 
				bring about revolutionary changes in peoples’ lives and habits 
				but is planned to generate an evolutionary response from the 
				target group to bring about permanent change in their attitude 
				over a period of time. After successful and effective campaign 
				that lasted for almost two years, TAC now plans to take the 
				message across to other group companies and to actively organise 
				and participate in interactive events implemented at ground 
				level across Oman as an ongoing exercise. It believes that 
				collective belief and action will encourage more and more people 
				to re-think about the impact of their actions on our environment 
				and help in reducing their carbon footprint in the world. <br>
