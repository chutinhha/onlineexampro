				</b><font face="Verdana" size="1"><br>
				The Omani women’s symposium, held in Sohar in October 2009, 
				during the Royal Tour, whose recommendations were endorsed by 
				His Majesty Sultan Qaboos bin Said, was a milestone in 
				empowering women in the Sultanate. It also underlined the 
				importance the government gives to women in nation-building. The 
				seminar’s suggestions would form the basis for future laws aimed 
				at safeguarding women’s rights while the existing rules would be 
				strengthened to enhance the role of national women in 
				professional jobs and businesses.<br>
				<br>
				One of the interesting outcomes of the symposium was to mark 
				Omani Women’s Day on October 17 of every year. Since the dawn of 
				the Blessed Renaissance, one of the top priorities of His 
				Majesty in nation-building has been education and the 
				empowerment of women. In the last four decades, women have 
				beaten new paths on the &#8232;socio-economic front. <br>
				<br>
				The emphasis in the country’s five-year plans is on realising 
				the full potential of women in their multiple roles as wives, 
				mothers, employees and social changers. In recent years, they 
				have taken up all the challenges confidently and are forging 
				ahead. Thanks to the government’s efforts and encouragement to 
				women to be part of the development process, they enjoy equal 
				opportunities with their male counterparts in the country’s 
				socio-economic life. <br>
				<br>
				Omani women have full civil and political rights, including the 
				right to vote, and they can stand for the membership of the 
				Consultative Council (Majlis A’Shura), State Council (Majlis 
				A’Dawla), the Board of Oman Chamber of Commerce and Industry and 
				other bodies. They have been proving their mettle as members of 
				the Sultanate’s executive, parliamentary, judicial and public 
				councils and authorities for many years. At the government 
				level, we find women in ministerial, diplomatic and official 
				positions, performing their work efficiently.<br>
				<br>
				Omani women are on a par with others in any developed country. 
				In fact, women’s rights have been guaranteed in the Basic Law of 
				the State. Article 12 of the statute affirms that “justice and 
				equality and equal opportunities for the Omanis are pillars of 
				the society and are guaranteed by the state.” <br>
				<br>
				As the country celebrates its first Omani Women Day, OER pays 
				tribute to the strides taken by women in the Sultanate and 
				celebrates their triumph.<br>
