                  Yes...to tourism</font>
                    <p><b><font face="Verdana" size="1">Festival 2003 arrives 
                    this month. Many families will be made happier through the 
                    efforts of organisers, but how much business will the event 
                    generate? OER investigate</font></b><font face="Verdana" size="1"><b>&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2">
                    <img border="0" src="fireworks.jpg" align="left" width="134" height="200">Yes...to Oman - is the official motto of the Muscat 
                    Festival 2003, which is being held between January 8 and 
                    February 14. Managed by Muscat Municipality, Oman's largest 
                    tourist extravaganza has been run three times in the past as 
                    a heritage event. And this year it is no different, although 
                    there are assurances that it will be bigger and better.<br>
                    'The festival is building its reputation. Though we have 
                    limited resources, we will drive home our message clearly,' 
                    says Ibrahim Abdullah Al Rahbi, Head of Promotions - Muscat 
                    Festival, Muscat Municipality.<br>
                    The festival in 2003, with added attraction and a thorough 
                    media plan in place, is expected to create a new attendance 
                    record of over 2.5 million people, compared to a million 
                    visitors attracted during the last festival. Some say that 
                    the festival may even bring in the much-needed commercial 
                    boost to 
                    <img border="0" src="muscat%20festival-1.jpg" align="right" width="75" height="112">Oman. Officials at Muscat Municipality observe that 
                    they will be able to deliver excitement to the international 
                    tourists and local residents whilst meeting the expectations 
                    of the local business community.<br>
                    'Muscat Festival 2003 is a forum where international 
                    visitors, Omanis and residents shall meet. This will enhance 
                    the quality of tourism and contribute towards domestic 
                    income,' states another official from the municipality.<br>
                    <br>
                    <b>Business or not?</b><br>
                    While some of the retailers and traders remain sceptical 
                    about any significant benefits that could be gained from the 
                    festival, curio shops, restaurants hotels and tour 
                    operators, anticipate better-than-average turnover during 
                    the festive period. Museums, forts and other heritage sites 
                    also have been tipped to generate good business.<br>
                    <br>
                    Underlining the concerns that exist in the local retail 
                    sector, a Muscat-based shopping centre manager, says, 'Many 
                    malls are eager to get business from the tourists during the 
                    working hours on weekdays, but such walk-in shoppers simply 
                    do not exist. Even though the government is working hard to 
                    attract more visitors, events such as the Muscat Festival, 
                    however impressive, still do not provide for big business 
                    that the majority of shops need to stay healthy.'<br>
                    <img border="0" src="muscat%20festival-2.jpg" align="left" width="92" height="137"><br>
                    To this Rahbi says that he does not believe the retail 
                    market will not grasp the opportunity to run more 
                    promotions, even though soon after Ramadhan. 'There is an 
                    element of commerce within the festival, but they need to 
                    plan to get it,' he explains.<br>
                    <br>
                    Muscat Municipality has been coordinating strategies with 
                    shopping malls, and events shall not be staged at the 
                    shopping centres. Earlier, the festival's activities choked 
                    the arterial routes around the shopping areas in Qurum. 
                    Retailers in the area voiced their concerns, and since then 
                    the municipality has organised the festival at different 
                    locations around the city. This year the areas include Qurum 
                    Natural Park, Naseem Natural Park, Azhaiba beach, and the 
                    festival grounds at Al Khuwair. <br>
                    HE Abdullah Abbas Ahmed, Head of Muscat Municipality, admits 
                    that the festival still has a long way to go before it can 
                    delight everyone. 'With every festival, we seek excellence 
                    as our best companion for our hard work, but unfortunately, 
                    success is never complete. For this we continue our efforts 
                    to increase our achievements.<br>
                    He further adds, 'In order to achieve the anticipated 
                    returns from the festival, the organising committee, in 
                    coordination with the authorities concerned, shall provide 
                    all the services that will facilitate the tourist flow. 
                    Besides, the private sector travel and tour operators shall 
                    provide various camping and adventure trips, attractive 
                    discounts, gifts and prizes for all the visitors throughout 
                    the festive period.'<br>
                    <br>
                    <b>A fresh approach<br>
                    </b>Muscat Festival, with all its objectives, is developing 
                    a new vision. It is a blend of culture, contemporary and 
                    originality together with trade and tourism, according to HE 
                    Ahmed. 'This was the case with previous festivals, 
                    reflecting our noble objectives in creating a traditional 
                    and cultural event that mirrors our social, cultural, 
                    historical, tourism, economic and art values,' he emphasises.<br>
                    Many expect that the festival will open the door to tourists 
                    from all over the world. Riding on the back of the Dubai 
                    Shopping Festival (DSF), which has moved its run between 
                    January 15 and February 15, instead of its usual 
                    February-March slot. Most believe that DSF might actually 
                    help Muscat to capture global tourists.<br>
                    Oman Air, for instance, has come out with an array of 
                    attractive tour packages. The national carrier, a gold 
                    sponsor of the Muscat Festival, has introduced combined 
                    packages for the festivals in Muscat and Dubai. The package 
                    from Muscat will be RO70 (US$182), while from Dubai it will 
                    be RO60 (US$156) that includes hotel accommodation for three 
                    days, an economy class return ticket, and other services.
                    <br>
                    Last month, a high-level delegation from the DSF had come 
                    down to Muscat to market the shopping festival. 'Omani 
                    visitors can go for shopping and enjoy the promotions 
                    available. They can also avail the benefits and incentives 
                    offered by the hotels and tourism establishments,' reported 
                    a DSF official.<br>
                    Also, a host of bargains, prizes and gifts are planned for 
                    the guests who visit Oman, but these may be nowhere near 
                    matching those on offer across the border. Nabeel Sultan, 
                    director with Jawad Sultan Enterprises LLC, says, 'It is 
                    important to determine the direction in which Muscat 
                    Festival is going to move during the next five years. The 
                    challenge is to be innovative, and I feel it is time to 
                    embrace tactics similar to that being adopted in Dubai, 
                    namely excitement, entertainment and shopping deals. But we 
                    should also tackle some underlying problems that exist in 
                    the economy. Only then, can we market the country to its 
                    complete potential.'<br>
                    Oman is a great tourism product, yet some are not sure where 
                    does the Muscat Festival fit within the overseas marketing 
                    strategy of the country. One airline in Oman regards the 
                    festival as a 'complete non-starter' for producing in-bound 
                    tourism.<br>
                    Ruedi Seiz of SWISS agrees, saying, 'Muscat Festival is one 
                    event which may attract people from the neighbouring GCC 
                    countries, but not tourists from Europe as there is much 
                    more activity on offer in Dubai for them.' <br>
                    'The drivers to Oman are sun, sand, wadis, cleanliness, 
                    heritage, the friendliness of local people and high-quality 
                    infrastructure. Muscat Festival does not contribute to any 
                    of these whatsoever. The focus for upmarket European tourism 
                    to Oman should be based on the development of, say, golf 
                    courses,' he adds.<br>
                    <b><br>
                    Target audience<br>
                    </b>A few company officials are of the view that since the 
                    event was originally designed for the locals, it benefited 
                    them as employees could take their families out on leisure 
                    trips.<br>
                    But will the upcoming event be more than a recreational 
                    outing for families in Oman? remains a question to be 
                    answered. The target audience is largely domestic but a 
                    blitz of advertising in pan-Arab countries, particularly in 
                    the GCC region, shows where the real strategy is. 'We are 
                    focusing on the spenders, like the GCC residents, and also 
                    those from the Indian subcontinent, Syria, Lebanon and so 
                    on,' says Rahbi of Muscat Municipality adding that the 
                    entertainment being planned is varied but it will mostly be 
                    for children.<br>
                    <br>
                    <b>Providing fizzle<br>
                    </b>The municipality has an ambitious plan to promote the 
                    festival in the local and international media, and it shows 
                    that it is not willing to count on DSF for its own success. 
                    Berit Media and Production FZ LLC is charged with the 
                    responsibility of making sure the festival's excitement 
                    becomes fever-pitch in the run up to zero hour. Rahbi is 
                    upbeat about how the momentum of publicity is gaining, the 
                    efforts of which include US$5 million worth of prizes as 
                    giveaway. The first week of the festival will announce 
                    winners of a quiz and lucky entrants can win cars, cash and 
                    electrical appliances. Dubai-based Berit has also secured 
                    many sponsors with the help of the Municipality including 
                    the Saud Bahwan Group as a platinum sponsor (which cost the 
                    company US$520,000).
                    <br>
                    An official source within the municipality says that direct 
                    expenditure on Muscat Festival is likely to touch RO500,000 
                    (US$1.3 million), whereas in the previous years it has been 
                    three times this amount.<br>
                    The value of advertising on television that Berit is 
                    offering sponsors is over US$3 million, although since the 
                    company has been volume buying, the exact spend is not known 
                    but is thought to be considerably less than the aforesaid 
                    figure. Advertisements for Oman's festival have already 
                    kicked off on MBC and Showtime, and will be joined with 
                    plugs on Dream TV, LBC, ESC, Zee TV, Space Toons, Future TV, 
                    Abu Dhabi TV and Oman TV. A radio media budget of around 
                    US$910,000 has also been set aside along with US$100,000 for 
                    interactive web-based promotions and close to US$2 million 
                    on press including newspapers in the GCC and pan-Arab 
                    dailies like Al Hayat, Laha and Al Idari. <br>
                    <br>
                    <b>Red carpet treatment</b><br>
                    No effort will be spared to provide the best service and 
                    comfort to all the visitors, residents and tourists expected 
                    to visit the Muscat Festival. The government is considering 
                    a range of new visa procedures to facilitate entry into Oman 
                    during the festival, even though many steps are yet to be 
                    confirmed by various ministries and the Royal Oman Police (ROP).<br>
&nbsp;Tentative plans include an entry visa made simpler and shorter with less 
                    information asked on a unified form. Applicants from over 70 
                    countries can obtain visa on arrival. Muscat Municipality is 
                    planning to print more than 30,000 new visa forms for 
                    distribution in hotels and travel agencies. <br>
                    More ROP officials and other special facilities may also be 
                    added to airport desks and border posts to ease access to 
                    and from the country and to welcome visitors to Oman during 
                    the festival. Meanwhile, in the UAE, visas will also be 
                    processed faster and easier. <br>
                    However, a better news in Muscat is that there may even be a 
                    possibility that the visa fee of RO7 will be waived during 
                    the festival period although the decision from the Ministry 
                    of National Economy on this had yet to be confirmed before 
                    OER went to press</font><font size="2">. </font></p>
