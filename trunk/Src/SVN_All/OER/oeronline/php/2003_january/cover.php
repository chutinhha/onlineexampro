            <img border="0" src="pdo-5.jpg" align="left" width="140" height="144">Can he 
                  deliver?</font>
                    <p><font face="Verdana" size="1"><b>With maturing oil fields 
                    and declining output levels posing a continuing challenge, 
                    2003 is expected to be a year of test for PDO. The company's 
                    newly appointed managing director, John Malcolm, gives Nayl 
                    D'Souza a low-down on his plans for reversing the trend&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2">Just a few months back, the government of Oman was left 
                    to reappraise its directives for development due to a 
                    substantial decline in the production levels of oil at 
                    Petroleum Development Oman (PDO). This fell against the 
                    Sultanates foremost plan that aimed to boost the oil output 
                    to over one million barrels per day (bpd). quick fix, 
                    however, is not the strategy being advocated by John 
                    Malcolm, who took over as the managing director of PDO in 
                    November 2002. The state-led company, which already unveiled 
                    its five-year turnaround plan last month, expects the first 
                    signs of recovery in output to become apparent only in 2004.<br>
                    <br>
                    Prior to PDO, Malcolm has worked with Al Furat Petroleum 
                    Company (AFPC) in Syria. Similar to PDO, AFPC had been 
                    suffering from falling production levels, as existing 
                    reservoirs had matured, making oil extraction difficult. 
                    'From a company that had suffered plummeting oil output for 
                    10 years, AFPC today has a plateau of sustainable oil 
                    production at around 300,000 barrels per day (bpd),' says 
                    Malcolm in an exclusive interview to OER.<br>
                    <br>
                    Achieving breakthrough improvements at PDO is not going to 
                    be easy. Malcolm declares that the management consultants 
                    would call this a defining moment in the company's history. 
                    'It is going to be extremely difficult to rebuild the 
                    company and establish oil production at the same levels as 
                    last year. We probably will not see many significant 
                    benefits until at least next year, but I am confident that 
                    we can change our management systems and implement 
                    strategies to reverse the trend of declining production,' he 
                    says. <br>
                    <br>
                    PDO's crude oil production level, which roughly accounts for 
                    40 per cent of the country's GDP and more than 90 per cent 
                    its output, has slipped by 10 per cent during the past 18 
                    months. The company's management is adamant that PDO will 
                    maintain its reputation as a world-class company and regain 
                    its levels of production. 'PDO is not in decline. The 
                    company is moving through a rough patch,' emphasises 
                    Malcolm. 'Normally, when an oil company has production 
                    difficulties, it reacts by adopting short-term measures. At 
                    PDO were trying to strike a careful balance between 
                    short-term optimisation and vast improvements in the long 
                    term.'<br>
                    <br>
                    <img border="0" src="black%20oil%20reserve.jpg" align="left" width="190" height="132">The commitment from PDO is considerable. Over the next five 
                    years, its capital expenditure will rise by 50 per cent, as 
                    US$1 billion will be spent on boosting oil and gas 
                    production, ongoing exploration activities and technological 
                    innovation. PDO is banking on regaining production levels of 
                    800,000 bpd by 2007. <br>
                    <br>
                    Over and above the Sixth Five-Year Plan (FYP, 2001-2005), 
                    Oman has increased its surplus in oil revenues and deposited 
                    the results of its fiscal prudence into the State General 
                    Reserve Fund (SGRF) that was formed to preserve national 
                    wealth for the future generations. The governments 2001-2002 
                    budget was based on a cautious assumption that the average 
                    price of oil would remain around US$18 per barrel. But, to 
                    the country's benefit, the average price for the Omani crude 
                    was actually US$23 per barrel in 2001, thus resulting in 
                    surplus revenue. But, the Sultanates oil production as per 
                    the Sixth FYP has been estimated slightly above 800,000 bpd. 
                    Bearing this in mind, there could be an affect on revenue if 
                    PDO fails to meet its new output commitments despite the 
                    fact that current crude oil prices are hovering above US$27 
                    per barrel for February 2003 delivery.<br>
                    <br>
                    Running the business in a slightly more efficient manner 
                    could pay extraordinary dividends. 'If you can optimise what 
                    youve got, a little tweak of one or two per cent in 
                    production can result in an additional generation of 15,000 
                    bpd on normal average volumes. And if you multiply that by 
                    the oil price today of US$24-25 per barrel, an extra income 
                    of US$360,000 per day can be generated,' adds a PDO 
                    official.<br>
                    <br>
                    Many of Omans privatisation and diversification projects 
                    have gathered pace due to extra oil income earned by the 
                    government. While a few projects have stumbled, others might 
                    already anticipate delays if the relative effect of less oil 
                    production begins to disrupt the governments budgetary 
                    planning. <br>
                    <br>
                    <b>Recovery is paramount</b><br>
                    Target 50 has been PDOs renewed mantra in recent times. It 
                    is an aspiration based on tapping at least half of the 
                    estimated 50 billion barrels of oil buried within the 
                    country. 'Fifty per cent is a good target for PDO to set, 
                    although it may be slightly misleading because some 
                    reservoirs will be only marginally recoverable while others 
                    will not be at least for a decade or two,' explains Malcolm.
                    <br>
                    <br>
                    Moreover, the technical and logistical obstacles are 
                    breathtaking. PDO hopes to meet its production target by 
                    using the latest technologies, particularly on ageing wells, 
                    which the company has many. 'PDO has been engaged for many 
                    years in applying new development strategy to boost its oil 
                    production,' says Malcolm. 'The pattern is set to continue 
                    and we have to seize the opportunities offered by applying 
                    improved techniques to raise oil output levels once again.'<br>
                    <br>
                    For PDO, missing the next wave of opportunity is certainly 
                    not an option. 'I think whenever you make any switch, there 
                    is a difficulty of knowing when exactly to react,' says 
                    Malcolm who uses the analogy of choosing a new car as a 
                    decision that in some cases is made too late. 'At the time 
                    PDO considered that the technology 'vehicle' it was using may 
                    deliver the oil it wanted. But the company got caught.' 
                    Given the mature and complex nature of some of PDOs oil 
                    fields, this slip was understandable but clearly the company 
                    cant afford to stumble repeatedly. The litmus test will be 
                    to see how the company fares in the first half of 2003.<br>
                    <br>
                    The company, by using Enhanced Oil Recovery (EOR) techniques 
                    like steam injection, anticipates to prove that there is 
                    additional five billion barrels of oil in reserve from the 
                    117 oil producing fields it has already discovered. The 
                    company also expects to extract one to two billion barrels 
                    of oil from its future explorations. <br>
                    <br>
                    <b>Catching the next wave</b><br>
                    The EOR programme poses a major cost-control challenge. PDO 
                    is currently piloting several EOR projects that are not 
                    expected to enter full-scale implementation until after 
                    2007. The two main EOR projects are at Mukhaizna and Harweel. 
                    At Mukhaizna, PDO would like to rely on thermal recovery, 
                    which uses superheated steam to raise the reservoir 
                    temperature that thins heavy oil and helps it to flow 
                    better. At Harweel, the company is trying out miscible-gas 
                    injection with a view to increasing the amount of oil being 
                    produced from a cluster of oil fields. Harweel is planned to 
                    go onstream in Q1 of 2004 and contribute around 10,000 to 
                    12,000 bpd.<br>
                    <br>
                    'Our plan is to conduct pilot projects at 
                    Mukhaizna and Harweel, as well as other fields during the 
                    run up to 2007. Later we shall scale up the project, based 
                    on the success of these pilot runs.'<br>
                    <br>
                    In the short-to-medium term, secondary recovery methods such 
                    as waterflooding will be used to enhance production. 
                    Waterflooding is not a new concept at PDO which already 
                    produces over 50 per cent of its output. The process 
                    involves injecting water to sweep through reservoirs, but 
                    some wells are more susceptible to waterflood measures than 
                    others. 'By our next board meeting in April 2003, we will be 
                    making recommendations as to where and when waterflood 
                    projects should go onstream. There will be a real rush to 
                    deliver results on this,' reveals Malcolm.<br>
                    <br>
                    <b>New field exploration</b><br>
                    PDO has had some success at the Zalzala field; two 
                    exploratory wells close to Sakhiya and Ghafeer have also 
                    been successful, according to the company. More money is 
                    also earmarked for exploration in 2003. Zalzala is targeted 
                    to initially produce 6,000 bpd of oil.<br>
                    <br>
                    'We need to be smart to push up oil discoveries. With a 
                    small amount of added investment we can actually locate new 
                    fields quickly,' observes Malcolm.<br>
                    <br>
                    <b>Stepping on the gas</b><br>
                    Oman has nearly doubled its gas reserves since 1992, and a 
                    more aggressive exploration effort has been planned by PDO. 
                    Currently, proven gas reserves are around 22 trillion cubic 
                    feet (tcf). Oman estimates that the country could contain 
                    more than 34 tcf in total.<br>
                    <br>
                    'It is easy to focus on oil alone, but we have plans to 
                    speed up the development of gas production in few years,' 
                    says Malcolm. PDOs investment in the gas business will 
                    increase to US$2 billion during the next five years. This 
                    will be spent on getting the newly discovered gas fields of 
                    Central Oman onstream earlier than before, as well as 
                    bringing forward the development of the Barik and Saih Rawl 
                    fields.<br>
                    <br>
                    <b>The HR factor</b><br>
                    The company's publicly stated goal for the past five years 
                    has been to achieve 100 per cent Omanisation by 2007. But 
                    the country has relatively little EOR experience, and PDO 
                    officials point out that outside expertise being pooled to 
                    assist the company may mean that it will not meet this 
                    target. But it will nonetheless come close to it. <br>
                    <br>
                    PDO attained 82 per cent Omanisation at the end of 2001 with 
                    3,470 Omani employees. The company's target for 2002 is to 
                    have at least 3,352 nationals working at the company. 'One 
                    hundred per cent Omanisation will be met soon while 
                    maintaining the existing standards,' says another company 
                    official. <br>
                    <br>
                    PDO will also be looking forward to focus more on improving 
                    the company's track record on staff safety, which suffered 
                    major setbacks during recent years. Eight deaths and 52 
                    accidents were recorded in 2000, and a further five 
                    fatalities occurred in 2001 as a direct result of 
                    road-traffic accidents.<br>
                    <br>
                    <b>Switching to SAP</b><br>
                    The SAP integrated 'enterprise' software went online in PDO 
                    at the beginning of 2002, and has been suffering from 
                    teething problems. One year on, its implementation is a 
                    proof that the correlation between productivity and capital 
                    investment is not as synchronised as some of us may expect. 
                    'A system like SAP is a huge change in the way you do your 
                    business. But the benefits are not incremental. Even at a 
                    time of low capital investment, workers may be getting 
                    increased productivity by using the system that was 
                    installed a few years ago to a greater affect,' says 
                    Malcolm.<br>
                    A tremendous amount of effort is being made to get the 
                    system working. A six-month study in 1998 concluded that the 
                    implementation of a company-wide SAP system to replace an 
                    increasing number of systems could yield annual savings of 
                    US$13 million.<br>
                    Whether the meticulous planning, capital expenditure and 
                    deployment of resources to meet PDOs challenge will pay off 
                    is anybody's guess, but the new managing director at the helm 
                    of the company appears to think that the odds are definitely 
                    in his favour.</font><font size="2"> </font></p>
