            <img border="0" src="kskumar.jpg" align="left" hspace="2" vspace="2" width="120" height="139"></b>Before you turn the page, let me quickly assure you that this page 
            will not be another reprint of an article published elsewhere or a 
            review of the latest bestseller on a managerial subject! Derived 
            from local experience, it is meant to be a section totally relevant 
            to Oman, where I hope to raise issues important to all of us. 
            Through the support of practising professionals and this medium, I 
            intend to create a platform that can be used to discuss and act on 
            these issues. <br>
            Managing in Oman has its unique challenges that can and need to be 
            addressed. But what are these unique challenges? I suspect that 
            depending on whom you talk to, you will get varying answers; 
            however, the underlying causes for many perceived challenges are the 
            same. <br>
            Oman's being a part of the Gulf Co-operation Council (GCC) and 
            member of the World Trade Organisation (WTO) has implications for 
            managing our businesses. The Kingdom of Saudi Arabia is by far the 
            largest domestic market in the GCC region, housing approximately 70 
            per cent of the total population. Not very surprisingly, then, many 
            multinational companies have chosen Saudi as their manufacturing 
            base. <br>
            Also, Dubai has successfully leveraged its open culture and 
            re-export options to attract foreign investment in addition to 
            positioning itself as a preferred destination for tourism, 
            conventions etc., with an attempt at becoming the regional hub for 
            IT and media activities. <br>
            All this is well known and the implication is that we have to 
            compete against competitors with the advantage of large domestic 
            markets. Further, changes in Oman's Agency Law, without any 
            corresponding changes in economically strong United Arab Emirates (UAE) 
            and Saudi Arabia, will require alteration in both business areas and 
            past practices.<br>
            The level of consumer expenditure within Oman, a big driving force 
            for economic growth, presents another challenge. UAE is, of course, 
            Oman's largest supplier with 28 per cent of total imports in 2001 
            (Customs statistics), and if you consider the difficulty of data 
            capture at the road borders, the actual percentage can be 
            significantly higher by perhaps 10 to 15 per cent. However, what is 
            not so well known is that seven million Omanis and approximately an 
            equal number of expatriates cross the border to and from Oman to the 
            UAE each year. Even after allowing for residents in places like 
            Buraimi, truck and bus drivers and so on, who cross the borders 
            regularly, the picture represents a major portion of Oman's 
            consumption expenditure. At an assumed RO 25 per visit per person, 
            the expenditure in the UAE would be RO350 million, and this implies 
            that a significant portion of non-essential consumption expenditure 
            is being diverted to the UAE. <br>
            Other challenges stem from Oman's large geographical area, the 
            relatively small market size and the need to provide jobs to the 
            rapidly growing number of Omani youth. A business in Oman has to 
            live with these realities although the impact varies from sector to 
            sector, and within a sector from company to company. How then do we 
            as managers keep our companies profitable and grow our businesses, 
            and most importantly, generate employment? <br>
            All of us, of course, try and meet these challenges within our own 
            organisations, and response strategies of different companies have 
            varied, as they must. Traditionally, response strategies have 
            included redefining business areas, focus on export (and in the case 
            of trading, re-export), seeking to totally dominate a sector or at 
            least ensure critical mass, cost reduction to offset limitations of 
            a smaller market and so on. <br>
            But the important thing is that our solutions must be homespun. I 
            have an additional proposition to put to you that I feel strongly 
            about - that we need to actively and effective collaborate both with 
            each other and with the government to get the best results. <br>
            Given the issues that we face and the general lack of transparency, 
            I feel a sense of urgency that we as managers need to act on 
            proactively and quickly. Current market leaders and market makers 
            may assume that collaboration is not necessary, but that will be a 
            myopic position indeed. While Omani companies must compete with each 
            other, we must seek areas of common interest to compete effectively 
            against our stronger neighbours and build on our strengths. <br>
            Let us consider some examples. If tomorrow the UAE imposes 
            non-tariff hurdles on export or re-export, it will certainly affect 
            Omani business and will require concerted joint efforts between the 
            government and the private sector to quickly resolve matters. While 
            reactive efforts can be easily understood, the recent combining of 
            logistics between Oman National Dairy Products and National Mineral 
            Water is a great idea for proactive collaboration to reduce cost and 
            compete better with the UAE and/or Saudi Arabia. There are many 
            possible areas for proactive action, and my objective will be to 
            serve as a catalyst to generate and aid in implementation of such 
            ideas.<br>
