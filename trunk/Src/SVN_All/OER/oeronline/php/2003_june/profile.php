                    Stainless stalwart<br>
                    <br>
                    </font></font><font face="Verdana" size="1">
                    <b>Vicky Kapur talks to Tawfeeq A. Al-Lawati, the 
                    effervescent chairman of Al Ahlia Detergent Company SAOG, 
                    and discovers a mind full of fizz </b><br>
                    <br>
                    <b>
                    <img border="0" src="personality.jpg" width="125" height="188" align="left" hspace="5" vspace="5"></b>The 
                    passion with which he talks about his company doesn't betray 
                    the fact that business wasn't his first choice of 
                    profession. It wasn't his second either. His father wanted 
                    him to become a civil engineer whereas he himself wanted to 
                    pursue medicine. He ended up doing neither. Meet Tawfeeq A. 
                    Al-Lawati, the chairman of Al Ahlia Detergent Company (ADC).
                    <br>
                    The first thing that strikes you about Lawati when you enter 
                    his office is his modesty. He walks up to take the door of 
                    his room when you knock, and insists that you be comfortable 
                    before he takes his seat. The second and more noticeable 
                    thing about him is the man's obsession with his company and 
                    its products. &quot;So what brand of detergent do you use at 
                    home?&quot; he asks as soon as you are through with the 
                    salutations. Excuse me, I thought I was supposed to ask the 
                    questions! &quot;It's more difficult to convince educated or 
                    senior people who might be status-conscious and shy to pick 
                    up an Omani product off the shelf in a supermarket,&quot; he 
                    offers an explanation. &quot;It isn't an issue of quality or 
                    performance. It is an issue of mindsets,&quot; he adds. <br>
                    And mindsets are what Lawati is out to change - mindsets of 
                    customers within the company and outside it. In a nutshell, 
                    the chairman of the RO5 million ADC is an unassuming man in 
                    his early-40s, educated in the US, well informed about the 
                    happenings in the world and has deep-rooted beliefs and 
                    opinions. <br>
                    After completing high school studies in Oman, Lawati flew to 
                    the US to obtain his undergraduate degree in Political 
                    Science &amp; Middle Eastern Studies from the University of 
                    Arizona. It was in the US that he decided to pursue his 
                    Masters degree. &quot;The Masters in Administration wasn't in 
                    Business Administration, but more of Public Administration,&quot; 
                    Lawati is quick to point out. &quot;So, my technical background 
                    is not in business, but by history, we are a business 
                    family,&quot; he explains. <br>
                    That business runs thick and fast in Lawati's blood is 
                    obvious from the fact that despite not having any formal 
                    qualifications in running a business, he has managed to make 
                    his company scale new heights and has done it in 
                    double-quick time. Consider this: ADC's Sohar plant, with a 
                    total capacity of 20,000 metric tonnes, began production in 
                    1996 with a capacity utilisation between 32 and 35 per cent. 
                    Today, a little more than six years later, not only is the 
                    plant functioning at full capacity, Lawati also has plans of 
                    doubling that capacity in the near future. <br>
                    <br>
                    <b>The detergents detour <br>
                    </b>Lawati returned to Oman in 1992 after completing his 
                    studies in the US and working there for a few years in 
                    companies as diverse as NGOs, auto dealerships and even 
                    marketing the products of an insurance company. Even though, 
                    historically, his family was into real estate, construction 
                    and trading business, &quot;with the government's support and 
                    encouragement for diversification and promotion of local 
                    industry, we went for the detergents factory,&quot; he says. <br>
                    &quot;It wasn't just detergents manufacture, but a dream to 
                    present Oman as a regional detergents centre,&quot; says Lawati. 
                    That that dream is still alive is obvious from the gleam in 
                    his eyes. &quot;For fulfilling that dream, we had three factories 
                    in our first plan,&quot; he adds. For the record, all three - 
                    ADC, Al Intaj Sulphochemicals Industries Co., and Oman 
                    Chemical Industries Co. - are now operational, an on track 
                    to fulfil that dream.<br>
                    The idea for getting into detergents was proposed by a 
                    &quot;family member,&quot; and with Lawati's contacts and experience 
                    in the GCC market, a feasibility study was conducted on the 
                    projects. &quot;We found it to be an interesting idea,&quot; he says. 
                    Moreover, he asserts that detergents presented a challenge 
                    for an Omani brand to make a mark in a market that was 
                    literally flooded with well-established multinational 
                    brands.<br>
                    Avers Lawati, &quot;Just a detergents factory by itself wasn't 
                    our end. It was meant to be a stepping stone for us for 
                    backward integration and introduction of other industries 
                    related to detergent manufacturing to Oman.&quot; Not 
                    surprisingly, then, ADC sources almost 50 per cent of its 
                    raw material from its sister concerns. &quot;We are the only 
                    integrated company in the Middle East or outside Europe that 
                    has such a high percentage of its feedstock being supplied 
                    in-house,&quot; claims Lawati. &quot;That has contributed to our 
                    success,&quot; he adds. <br>
                    <br>
                    <b>The path to success<br>
                    </b>Success, however, wasn't easy to come by. &quot;When we 
                    started out, people were comparing us with other existing 
                    industries and expecting dividends and returns from Day 1,&quot; 
                    he says. But it is a different ballgame in the detergents 
                    industry, he explains. &quot;It takes almost a year to qualify 
                    for GCC tax exemption.&quot; You ought to be operational for six 
                    months, have to declare your results, apply to the Ministry, 
                    that will approve it and then apply to the GCC secretary 
                    general who will, in turn, clear the proposal and inform the 
                    GCC states which will accept and intimate the borders for 
                    the product to be finally duty exempt. <br>
                    So, after not exporting for the first year as planned due to 
                    the high rate of duty applied on products by companies that 
                    aren't approved as exporters, ADC incurred losses on account 
                    of market penetration expenses. &quot;Some countries defer these 
                    expenses for a few years, but the high cost of launching a 
                    product in the GCC market and the infrastructure costs 
                    incurred took their toll,&quot; laments Lawati. <br>
                    These added up to huge losses on the initial capital of 
                    RO900,000. &quot;We absorbed these losses until we got 
                    established in the market and, in 1998, the company made 
                    cash profits for the first time,&quot; says Lawati. &quot;It's quality 
                    that we focus on - quality of the products, of the process, 
                    of the raw material and of the people - and that's what led 
                    to us making net profits consistently since 1999,&quot; he 
                    gleams. <br>
                    <br>
                    <b>Mission quality <br>
                    </b>But instilling quality in the core of the corporate is 
                    no easy task, as management gurus the world over would have 
                    you believe. So what is it that Lawati is doing in ADC for 
                    it to result in this quality movement? &quot;For one, we ensure 
                    that no individual is indispensable to the company's 
                    functioning. That way, the company stays above any person 
                    working for it, and does not suffer when that person, 
                    however high in the hierarchy he might be, leaves,&quot; 
                    proclaims Lawati. <br>
                    Lawati considers his role as one of providing support and 
                    guidance to the management of the company and sees himself 
                    as the safe-keeper of the trust of the shareholders and 
                    other stakeholders in the company. <br>
                    &quot;I am a hands-on person, but I don't like to interfere in 
                    what I believe is somebody else's domain,&quot; he asserts. &quot;In 
                    fact, it is the opposite. Sometimes, managers find it safer 
                    to take me into confidence on issues I'm not supposed to be 
                    referred to. At times like these, I tell them &quot;Look, you 
                    have the authority, and you must exercise it. I have the 
                    right to ask you questions' and then expect justified 
                    answers,&quot; he adds. <br>
                    <br>
                    <b>Economic perspective <br>
                    </b>&quot;We have to be more dynamic in our approach and reaction 
                    to the developments in the global economy,&quot; feels Lawati. 
                    &quot;Oman, being a young economy, requires support and backing 
                    of the State in the form of minimisation of bureaucracy and 
                    hurdles,&quot; he adds. He further opines that while the 
                    government is doing enough for the large industries in the 
                    Sultanate, still more needs to be done to ensure survival 
                    and prosperity of the small and medium enterprises. <br>
                    &quot;Even after being in existence for more than 150 years, a 
                    lot of industries in Europe and America are subsidised both 
                    directly as well as indirectly. So, it won't be right in the 
                    young economy of Oman to tell all these small industries to 
                    fight the deep pockets of multinationals without any 
                    resources.&quot; In this respect, Lawati favours an &quot;equal-level 
                    dialogue&quot; between the private sector and the government to 
                    improve the economy and find out what are the challenges 
                    ahead and how to overcome them.<br>
                    <br>
                    <b>Personal aspect <br>
                    </b>Lawati is a happily married man with three daughters and 
                    two sons, the youngest of whom was just a week old at the 
                    time of the interview in May. &quot;We are a big family now,&quot; 
                    exclaims a visibly happy Lawati. By his own admission, 
                    Lawati finds little time for his family, but tries to &quot;spend 
                    as much time as possible with them.&quot; The ADC factory is 
                    located in Sohar, about 250 km from Muscat, where he stays, 
                    and Lawati pays a plant visit every week. Not surprisingly, 
                    then, he is engaged in travel for more than 170 days in a 
                    year. The places he visits often during vacation with his 
                    family include Saudi Arabia and Europe. &quot;My wife's family is 
                    based in Saudi, and we end up visiting the country almost 
                    twice every year to attend marriages and other family 
                    affairs,&quot; he says.<br>
                    Although Lawati doesn't do a lot of what he terms 
                    &quot;horizontal reading,&quot; he loves to read in-depth on 
                    management and marketing. &quot;My latest read is Peter Drucker's 
                    Managing The Non-Profit Organization.&quot; Lawati also leads a 
                    busy social life, with active interests in swimming, 
                    walking, hiking and camping.</font></p>
