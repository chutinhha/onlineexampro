            <b>NATIONAL BANK OF OMAN</b></font></font><font size="4" color="#FF0000" face="Verdana"><br>
            </font><font face="Verdana" size="4">THE WORST IS OVER!</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Despite some 
            more setbacks for NBO, there are many positive signs that the bank 
            will weather the magnitude of exposure to bad debts that has dogged 
            its earnings in recent years<br>
            <br>
            </b>It might have celebrated thirty years of banking in 2003 but 
            National Bank of Oman<img border="0" src="john.jpg" align="right" vspace="2" hspace="5" width="100" height="136"> (NBO) has had a chequered history of late. The 
            bank continues to pay a heavy price for some less than judicial 
            lending and the related burden of provisioning for nonperforming 
            loans. The overall outcome of the bank’s operations for 2003 was a 
            huge net loss of RO51.7 million as a direct result of an increase in 
            loan loss provision compounded by an extraordinary charge in respect 
            to an ongoing dispute between the bank and the Central Bank of 
            Egypt. Understandably, Khalfan Nasser Al Wohaibi, the chairman of 
            NBO, says that the performance was “very disappointing”. <br>
            <br>
            He remarks that, ultimately, it was the “ill fated expansions of 
            credit operations, especially in the overseas branches [Egypt and 
            Abu Dhabi], in the latter years of the 1990s” that were to blame for 
            the less than satisfactory results for the bank’s operations three 
            years in succession (the bank posted a net loss of RO262,426 a year 
            ago and was in deficit to the tune of RO7.7 million in 2001). <br>
            <br>
            <img border="0" src="infor3.jpg" align="left" vspace="2" hspace="6" width="200" height="305">Despite passing on a dividend to long- uffering shareholders for a 
            third consecutive year, however, some market analysts are reasonably 
            bullish about the prospects for NBO. Officials at the bank are 
            equally adamant that the worst appears to be over and the good times 
            are just around the corner. Grappling to reverse the untoward 
            developments of recent years, the bank is particularly keen to 
            eradicate credit deficiencies and other under-performing elements in 
            order to restore profitability and offer a dividend, albeit in 
            modest percentages, at the soonest opportunity. “Based on the 
            actions which were taken during last year, NBO is well positioned to 
            generate a sustained level of profitability from 2004 onwards,” says 
            John P. Finigan, chief executive officer, NBO. “In spite of the 
            challenges faced in terms of oil price volatility, interest and 
            exchange rate variations both globally and in the countries in 
            whichthe bank operates and ever increasing competition we are 
            cautiously optimistic of being able to achieve our targets.” <br>
            <br>
            The company’s turnaround is sure to be boosted by the bank’s twin 
            strategy of prudent profitable business development and dynamic&nbsp; 
            remedial management. On the latter policy, more stringent credit 
            management and underwriting standards began in 2001. NBO maintains 
            that it is using “the full vigour of the law and due legal process 
            in each of the jurisdictions in which it operates to ensure the 
            unbridled pursuit of recalcitrant debtors”. In turn, this would 
            improve the bank’scollateral coverage and enhance the cash flow 
            available to discharge these obligations. </font>&nbsp;<br>
            <br>
            <font face="Verdana" size="1">Lately, NBO has been successful in 
            maximising recoveries. In fact, payments collected in respect of 
            indebtedness increased three-fold to RO18.56 million in 2003 from 
            RO6.3 million a year earlier. “Remedial efforts will be accelerated 
            in 2004 as we focus on collections and avoid the unacceptable 
            pitfalls created by lax lending standards of the past,” adds Finigan. 
            “Cash and balances with Central Banks increased by RO78.8 million 
            whilst the loan portfolio was reduced by RO81.7 million as part of 
            the qualitative improvement exercise to forestall the likelihood of 
            further impaired indebtedness arising in the future,” notes Al 
            Wohaibi.<br>
            <br>
            Another encouraging sign for NBO last year was theenhancement of the 
            bank’s capital made possible through an Equity Private Placement of 
            RO53.2 million including a share premium of RO29 million by Suhail 
            Bahwan Group (Holding) LLC, one of Oman’s leading industrial groups. 
            Following the injection of these additional funds in October 2003, 
            future provisioning requirements were immediately addressed. 
            Moreover, the paid-up capital of the bank rose to RO70 million and 
            the net worth after adjustments for losses during 2003 stood at 
            RO97.2 million. The bank’s market capitalisation increased by RO80.6 
            million (143 per cent) after an allowance for the aggregate equity 
            injection. Shareholder value in NBO was also given a critical shot 
            in the arm. Mostly as a consequence of the equity deal, the bank’s 
            share price appreciated by 102 per cent from RO1.340 at the 
            beginning of 2003 to RO2.70 at the year-end. By comparison, the MSM 
            market index rose by 42 per cent during the year. Clearly, the NBO 
            share price increase during 2003 was a remarkable feat given the 
            bank’s poor overall performance.<br>
            <br>
            During 2003, NBO’s single branch in Abu Dhabi focused on deposit 
            generation. “Corporate lending has now been reactivated but in a 
            selective manner with emphasis on credit quality. We are confident 
            we can prudently grow the business and profitability of this 
            branch,” maintains Finigan.<br>
            <br>
            In Egypt the situation is far less clear. The Egyptian economy has 
            been facing difficulties&nbsp; as the country seeks to establish a 
            firmer monetary base from which an export led growth strategy can 
            evolve accompanied by higher tourism revenues. For NBO, the strategy 
            for its 5 branches in Egypt has been to focus on deposit generation 
            and expense reduction. “We remain committed to the Egyptian market, 
            but will retain a cautious approach to any business expansion until 
            there are clear signs that economic conditions are improving.” <br>
            <br>
            NBO in 2003 also consolidated its Consumer Banking activities into a 
            single division encompassing sales, product development, credit 
            services and operations. The purpose of the reorganisation was to 
            improve customer service and satisfaction levels with better product 
            delivery. As part of the changing organisational structure, 6 
            regional manager posts were created to make swifter business 
            development decisions in their regions.<br>
            <br>
            NBO’s major customer-focused initiatives launched in 2003included 
            the Al-Ahlam personal loans programme, a co-branded credit car with 
            Oman Air, the Visa Electron Cards, 5 new ATM’s bringing the network 
            to 74, and new branches in Sohar Industrial Area and a booth at Seeb 
            International Airport. Following on from the landmark achievement in 
            2002 whereby NBO became the first bank in the country to bring 
            Internet banking to its customers with a new facility called NetB@nk, 
            IT has continued to play an important role in gaining custom and 
            delivering timely cost-efficient products to customers. “Key 
            initiatives in 2003 included the launch of the ATM and POS Debit 
            Card, the introduction of a transaction download feature in our 
            Internet Banking Channel, the upgrade of our Trade Finance System, 
            the introduction of replication software in our core system to 
            ensure high availability of the core systems and enhance disaster 
            recovery capability,” says Finigan.</font><br>
            <br>
            <font face="Verdana" size="1"><b>Appraising Human Resources</b><br>
            The bank looks forward to a future filled with exciting 
            opportunities for managing its most valuable resource – its people. 
            Recognising the need for radical change, as compensation and reward 
            significantly affects employee productivity, morale and motivation, 
            resulting in the achievement of organisational goals, NBO completely 
            re-vamped its HR policies in 2003. With “people” as the central 
            focus, NBO reviewed and revised its compensation and benefits 
            structure and in keeping with international best practice, 
            introduced a more effective grading system, reducing the number of 
            grades from 27 to 9. NBO also introduced a new Performance Appraisal 
            system, which effectively measures staff against specific goals, 
            allowing for transparency, with an emphasis on career development. 
            These initiatives have resulted in a more motivated banking staff, 
            92.96 per cent of whom are mani. To bear testimony to this, the bank 
            claims it has a significant number of exemployees coming back to NBO. 
            An equally important benefit is the continuous, qualitytraining that 
            NBO provides to its workforce. The bank believes that training is an 
            investment that pays rich dividends to the individual and the 
            organisation and helps achieve high levels of competency. In 2003, 
            NBO devoted more than 5 per cent of its staff cost to training, 
            creating over 4,000 training opportunities with an average of 7 
            training days per employee. <br>
            <br>
            <b>The future</b><br>
            NBO has had another bad year but there is evidence that the reforms 
            of control standards are working and it has scored a number of other 
            achievements to keep some anxiety at bay. It will, however, be 
            interesting to observe what level of profitability the bank can gain 
            in 2004. The harsh impact of economic adversity in Egypt will play 
            its part in determining how successful the bank will be in producing 
            a turnaround as well as the non-performing assets scenario. In the 
            meantime, it’s promising to see the bank increasingly get involved 
            in project financing activities as planned. Recently, NBO was lead 
            manager on two syndications on behalf of Oman Investment and Finance 
            Company (OIFC) for RO24.5 million and Gulf International Bank for 
            RO4.4 million.</font><br>
