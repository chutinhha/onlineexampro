            BANKMUSCAT</b></font><font size="4" color="#FF0000" face="Verdana"><br>
            </font><font face="Verdana" size="4">STRENGTH AND STATURE</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Determined to 
            successfully pursue its ambition to become a regional powerhouse, 
            BankMuscat’s one-stop approach to consumer banking and its expertise 
            in project finance as well as corporate lendingis producing great 
            results</b><br>
            <br>
            <br>
            Still in second spot in the OER 20 is BankMuscat, the largest bank 
            in Oman
            <img border="0" src="abdulr.jpg" align="right" vspace="2" hspace="7" width="100" height="135">with 
            assets over RO1.5 billion and an overall income in 2003 of RO120 
            million. As the bank continues to set its sights on becoming one of 
            the foremost banks across the region by the turn of the decade, it 
            will be encouraged by a favourable fiscal performance last year. The 
            bank posted a net profit of RO27.1 million, an increase of 18.3 per 
            cent compared to 2002. BankMuscat has been steeped with praise in 
            recent years. It has received yet more deserved recognition for its 
            financial performance last year. Euromoney, The Banker and Global 
            Finance magazines again declared it the ‘Best Bank in Oman’ while 
            Standard &amp; Poor’s also assigned ‘Investment Grade’ to BankMuscat, 
            the first full interactive ratings assigned to an Omani bank by the 
            international rating agency. When reviewing the fiscal triumphs for 
            2003, you begin to understand why the bank has been getting so much 
            praise. An increase of net interest income by 10 per cent to RO68.8 
            million, a surge of 15.9 per cent in non-interest income to RO19.6 
            million, and a 15.3 per cent jump in operating profit to RO48.9 
            million despite a 6 per cent rise in operating expenses to RO39.7 
            million. Customer deposits grew by RO20 million and the bank’s net 
            loans and advances by RO18 million. However, the success of 10-year 
            unsecured bonds raising RO25 million for BankMuscat was probably the 
            icing on the cake.<br>
            <br>
&nbsp;AbdulMalik bin Abdullah Al-Khalili, chairman of BankMuscat, confirms the 
            bank’s excellent track record, saying, “BankMuscat commands a market 
            share of 34 per cent in terms of total assets, 37.7 per cent in 
            terms of total credit and 34 per cent in terms of total customer 
            deposits.”<br>
            <br>
            <b>2003 in retrospect</b><br>
            OER readers will recall that last year AbdulRazak Ali Issa, chief 
            executive of BankMuscat,
            <img border="0" src="infor2.jpg" align="left" vspace="3" hspace="5" width="200" height="320">had 
            outlined the bank’s three-pronged strategy for success in 2003. He 
            explained that the bank would “selectively grow business in areas 
            where the return on risk was better”. Well, the bank certainly 
            managed to do this with the major contributor to the bank’s 
            accomplishments coming from its retail strength. BankMuscat has a 
            network of 90 branches in Oman which last year achieved a 28 per 
            cent growth in savings deposits. The bank claims the Al Mazyona 
            deposit scheme for savings account holders is the most popular of 
            its kind. Meanwhile, its personal and consumer loan portfolio has 
            increased by 10 per cent, a rise puffed up by the bank’s acquisition 
            of Citibank’s consumer loan portfolio. The bank also enhanced its 
            portfolio of credit cards by introducing a Platinum card in 
            association with MasterCard International. Together with 
            relationships forged by the bank’s private banking division, the 
            bank has continued to build stronger ties with high net worth 
            individuals. <br>
&nbsp;Secondly, AbdulRazak said the bank would “seek to improve its asset 
            quality and accelerate the recovery of doubtful debts”. So, how did 
            Bank Muscat fare? Despite a 5 per cent increase in loan loss 
            provision to RO21.8 million in 2003 as compared to a year earlier, 
            it successfully managed to recover RO6.1 million from classified 
            assets in 2003, arise of 30 per cent.<br>
            <br>
&nbsp;Thirdly, AbdulRazak revealed that BankMuscat would aim “to reduce costs 
            through the use of appropriate technology in operations and delivery 
            channels”. The bank currently offers a basket brimming with 
            e-banking products as well as customer conveniences such as a call 
            centre, Internet banking, and largest network of ATMs in the country 
            totalling 152.<br>
            <br>
            Key initiatives during 2003 completed by the bank included 
            “revamping the core banking system in Bahrain, preparations for the 
            launch of Mobile Banking services in Oman and the upgrade of 
            security levels around the bank’s ATM network to provide higher 
            levels of security to customers.” The company’s 2003 annual report 
            also highlights the investment being made to implement a robust and 
            comprehensive Business Continuity Plan. The project is “progressing 
            at a satisfactory pace” and should be completed by 2004. Such a 
            system incorporates technology advances to minimise the impact of 
            operational risks.<br>
            <br>
&nbsp;Other aspects of the business have also grown steadily for BankMuscat. The 
            bank’s brokerage division claims to have a leadership position in 
            the market despite increasing competition from other banks and 
            consolidation among other brokerage companies while the bank’s asset 
            management services grew beyond expectations during 2003. In fact, 
            the Asset Management division currently manages about RO45 million 
            through mutual funds and client portfolios and dominant market share 
            of 88 per cent in the local mutual funds industry in Oman. All in 
            all, the strategy BankMuscat has been adopting has unquestionably 
            paid dividends. And for the bank’s shareholders, this literally 
            equated to a 25 per cent cash dividend and a 5 per cent stock 
            dividend. <br>
            <br>
            <b>Overseas developments</b><br>
            Aside from its operations in Oman, BankMuscat’s branch in Bahrain 
            achieved excellent results during 2003 and is establishing itself 
            well in spite of a highly competitive Bahraini market. According to 
            the bank, it is now well poised to cater to the current and emerging<br>
            needs of the discerning retail customer in Bahrain with a new IT 
            system in place and a range of retail offerings underway. The bank 
            also has a representative office in Dubai (UAE) and a growing 
            presence across the region. Moreover, it also recently acquired a 26 
            per cent stake in Centurion Bank, a new private sector bank in 
            India. The BankMuscat branch in Bangalore subsequently merged with 
            Centurion bank as of April 1, 2003. Sheikh AbdulMalik maintains: 
            “BankMuscat’s ability to benefit from the fast-growing Indian sub- 
            ontinent will be significantly increased by this new arrangement.” 
            Areas of mutual interest that could be looked at between BankMuscat 
            and Centurion Bank in the short- and medium term include trade 
            finance opportunities between India and the GCC region, the 
            burgeoning non-resident Indian business and investment flows.<br>
            <br>
            <b>The future</b><br>
            BankMuscat reports that it earmarks a significant amount of money 
            towards staff evelopment with tailor-made training programmes to 
            cater to the skill enhancement needs of individuals. n Oracle-based 
            HR interface is in its final stages of implementation and testing. 
            The interface will allow staff members to interact with the HR 
            division from the comfort of their workstations, streamline 
            processes and smoothen career planning and knowledge management 
            programmes for every employee. The bank had achieved a 90.6 per cent 
            level of Omanisation by the end of 2003. In 2004, BankMuscat has 
            plans to add 5 branches to its domestic network as part of its 
            continued focus on improving its already sparkling retail feats. 
            BankMuscat opened its innings this year with the launch of 
            BankMuscat eTrade, the first comprehensive suite of Trade Finance 
            services on the web to&nbsp; be developed and introduced by a Gulf 
            Bank. BankMuscat eTrade is expected to change the very way people 
            look at the trade finance business, as it will go on to impact all 
            the stages of the business from online documentation and transaction 
            tracking to settlements online. In February this year, the bank 
            launched the BankMuscat MasterCard Purchasing Card, the first 
            corporate procurement card in Oman that enables enterprises in Oman 
            to do business in Oman faster and more efficiently by minimising 
            paperwork and reducing staff involvement on slow moving procurement 
            procedures. Petroleum Development Oman are among the customers that 
            have joined the bank’s purchasing scheme and expect significant 
            savings to be made from its introduction at the company. Moreover, 
            the bank is arranging an $800 million Euro Medium Term Note 
            programme (EMTN), which will be issued overseas and listed on the 
            Luxembourg stock exchange. BankMuscat has also repackaged Al Mazyona 
            savings scheme with a bigger prize kitty and additional excitement 
            for the customers, which came into effect from April 15. The bankhas 
            upped the bumper quarterly prizes it offers in the scheme from 
            RO100,000 to RO150,000. No doubt, the larger prize payout will be 
            successful in generating additional savings and savers for the bank 
            through the scheme. In conclusion, BankMuscat’s ‘power banking’ 
            formula looks like it could withstand any potential threats better 
            than most.&nbsp; And that strength speaks for itself. <br>
