            Muscat and Salalah because of Khareef season. This is offset 
            slightly by the decline in Muscat which, of course, suffers because 
            many expatriates have returned home for the holiday season.<br>
            <br>
            Due to the huge rush, there is a strain on the bus network but we 
            ensure our passengers are comfortable and we never say no to our 
            customers. We provide extra coaches to meet<br>
            with customer demand and we handle all the personal baggage that the 
            extra business brings. In fact, we operate special cargo services to 
            ensure passenger services remain safe, convenient and comfortable 
            for all who travel with us.<br>
            <br>
            For those travelling to the UAE by bus, the compulsive movement of 
            customers has been adversely effected by the Dhs100 (RO10) visa 
            charge at the UAE border post. There is also a case for speeding up 
            the visa process at the new exit/entry points which add time to our 
            coach journeys.</font></p>
