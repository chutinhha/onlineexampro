            million passengers it serves each year. All public transport buses 
            here are airconditioned. Additionally, some of the luxury coaches 
            which our company has acquired have state-of-the-art amenities which 
            many parts of the Middle East have yet to experience. All in all, 
            the company is growing into an exciting prospect for would-be 
            private investors.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>With budget air 
            travel appears as cheap as the buses, why choose to travel by road?</b><br>
            I believe low-income families need to be offered a choice. This is 
            particularly important in Oman where families are larger than in 
            other countries. ONTC provides reasonably priced,<br>
            comfortable and safe journeys as an alternative to other modes of 
            travel. The new budget airlines that are offering introductory 
            prices to entice customers are probably not sustainable in the long 
            run. When you look at the ease of flying as compared to bus travel, 
            there are many more conveniences with coach travel. For example, 
            ONTC offers more frequencies than budget airlines. We run express 
            services like those from Muscat to Dubai three times daily at 
            convenient timings. In addition, we are finding that Omani families 
            that used to be reluctant to use coaches for long journeys are using 
            them more frequently. Women and children are especially more 
            confident to travel alone nowadays. We ferry them to all areas of 
            Oman, particularly Sur, Ibri and Buraimi while our service to 
            Salalah from Muscat transports around 75,000 customers per year.</font></p>
