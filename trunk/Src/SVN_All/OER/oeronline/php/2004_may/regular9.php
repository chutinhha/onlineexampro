            coaches. Our company operates a public transport service throughout 
            Oman and to the UAE. The majority of our business is focused on 
            delivering charter services. Contract services are of paramount 
            importance because they are profit making and subsidise running the 
            public transport network within the country which is generally loss 
            making. <br>
