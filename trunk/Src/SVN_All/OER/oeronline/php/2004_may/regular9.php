            system because congestion is on the increase here and a recent 
            report noted that average speeds during peak hours on roads may 
            reduce to 8km/h. The best solution to this potential problem is to 
            invest more on public transport. A sound public transport policy 
            will alleviate future congestion and parking problems in Muscat.</font></p>
