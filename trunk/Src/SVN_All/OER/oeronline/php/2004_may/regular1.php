<p align="justify"><font face="Verdana" size="1"><b>After half a decade of 
cooperation, OPEC members are once again at odds over policy, says Richard Dean</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im5.jpg" align="left" width="200" height="187">Since the 
late 1990s, OPEC members have all but dispelled the myth that the organisation 
is a spent force. Back in 1998, many oil market watchers were writing OPEC's 
obituary after bitter infighting saw cooperation fall apart and oil prices fall 
to $10 (RO3.85) per barrel (/b). But in March 1999, members buried the hatchet, 
agreed to work together to manage oil supply, and the result has been a period 
of almost unprecedented harmony. Prices have been way above $20/b (RO7.69) 
almost every month since the turn of the millennium.<br>
<br>
Today, though, that truce is under threat: OPEC is in the midst of its biggest 
row for nearly a decade. Heavyweight producer Saudi Arabia has squared up 
against mid-weight opponents Kuwait and the UAE. OPEC is already showing signs 
of breaking at the seams - and some OPEC watchers are warning that the spat 
could escalate into a full-blown rift that could leave the cartel's supply 
management policy in tatters.<br>
<br>
The row relates to controversial OPEC output cuts implemented on April 1 this 
year. In February, members met in Algiers and agreed to a 1-million barrels per 
day (b/d) output cut from April 1. The move was a response to fears that the 
traditional second quarter ease in demand would lead to oversupply, and a 
consequent sharp drop in prices. <br>
<br>
However, by late March, prices of crude in the US had topped $38/b (RO14.62), 
its highest level for 13 years. Obeid bin Saif Al Nasiri, UAE's minister of 
petroleum and mineral resources, along with his Kuwaiti counterpart Sheikh Ahmad 
Al Fahd Al Sabah, argued that to cut supplies with the price so high would risk 
sending the price even higher. Their proposal was partly a reaction to US claims 
that high oil prices were hurting the US and world economic recovery. <br>
<br>
OPEC members led by Saudi Arabia rejected Al Nasiri's plan, and the cuts were 
implemented. &quot;As far as Saudi Arabia is concerned, April 1 cuts have been 
implemented and I believe others have done so as well,&quot; said Saudi oil minister 
Ali Al Naimi. &quot;Throwing more oil on the market, because of prices where they are 
today, would be destructive. That would make a glut and there is already a 
surplus on the market.&quot; Saudi Arabia, Libya, Iran and others argued that 
postponing the cut would allow inventories to rebuild too quickly, risking a 
sharp downward correction in crude prices on world markets.<br>
<br>
The UAE's Al Nasiri accepted their decision - but only grudgingly. &quot;This is the 
decision taken, we have to respect OPEC's decision,&quot; he said tersely. As April 
unfolded, though, it became increasingly clear that the UAE and Kuwait had a 
valid point: OPEC's policy was beginning to look ridiculous. In mid-April, OPEC 
president Purnomo Yusgiantoro confirmed that the group's 10 members that have 
quotas (excluding Iraq) were pumping excess crude oil to bring down prices. In 
effect, OPEC's official policy (cutting production) is at direct odds with its 
unofficial policy (boosting production), making it something of a laughing stock 
and undermining its credibility.<br>
<br>
A quick look at the figures underlines the problem. Output by the OPEC 10 rose 
10,000 b/d to 26.1 million b/d in March, according to a Bloomberg survey. That 
was 1.57 million barrels, or 6.4 per cent, above the producers' March quota of 
24.5 million. From April 1, that quota fell to 23.5 million b/d - but as Purnomo 
confirmed, members are largely ignoring the diktat. &quot;OPEC members are still 
producing above quota because we want to bring down the oil price to between $22 
and $28/b (RO8.46 - RO10.77).&quot; This target price is for a basket comprising 
seven crude oils. The OPEC benchmark has been above that range since December 
2003, hovering around $32/b (RO12.31) in mid-April.<br>
<br>
&quot;OPEC is in danger of losing its credibility,&quot; one Dubai-based oil analyst told 
OER. &quot;The left hand is cutting quotas to support prices while the right hand is 
telling members to pump more oil to reduce prices. <br>
<br>
This could be very damaging for OPEC in the medium term. It is able to influence 
markets and prices these days because traders believe that when OPEC says it 
will do something, it will follow through on that pledge. If it keeps sending 
out mixed messages like this, traders are going to pay less and less attention 
to it.&quot;<br>
<br>
Is it time to reach for the typewriters and once again begin crafting an epitaph 
for OPEC? Probably not. Independent diplomatic sources in Abu Dhabi played down 
talk of a serious rift between Saudi Arabia and the UAE. Some commentators have 
suggested the split could signal the end of six years of relatively strong 
cooperation among OPEC members. But sources in Abu Dhabi pointed out that all 11 
OPEC members (including Iraq) continue to support the same broad goal - 
sustaining prices for the OPEC crude basket between $22 and $28/b. The 
differences are simply about the tactics for achieving this shared goal in the 
short term. <br>
<br>
This may paint too rosy a picture of relations between OPEC members. But the 
fundamental point is that it would be wrong to write off OPEC just yet. Many of 
today's OPEC oil ministers were around in 1998 and 1999 when prices nosedived. 
They have not forgotten the heavy price their countries paid for letting petty 
rivalries disrupt policy back then. Indeed, their counterparts in the finance 
ministries will not let them. While a few internal disagreements are inevitable, 
the stakes are too high for these to lead to a major schism.<br>
<br>
<i>The author is Director, Middle East Analytics, a London-based economic 
consultancy.</i></font></p>

