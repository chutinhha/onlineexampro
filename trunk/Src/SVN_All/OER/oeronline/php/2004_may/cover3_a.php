            <p align="justify"><font face="Verdana" size="1">
            <b>Shell Oman Marketing Company retains its pole position in The OER 
            20 but there are tough times ahead. The ‘gasification’ of Oman’s 
            power stations and overall declining trend in wholesale margins has 
            hit the company hard, a profit warning for 2004 has been sounded and 
            the competition in the local market keeps intensifying</b><br>
            <br>
&nbsp;Shell Oman Marketing Co. SAOG<img border="0" src="irs.jpg" align="right" width="100" height="135"> 
            is once again the Sultanate’s largest listed company, albeit for 
            2003. Although Al Maha Petroleum Products Marketing Company’s entry 
            into the local stock exchange will not dislodge Shell Oman fromits 
            coveted top spot in OER’s ranking next year, the looming public 
            issue of shares in Omantel probably will. In the meantime, as far as 
            Shell Oman’s fiscal performance in 2003 is concerned, shareholders 
            would have been glad to hear that the company had achieved sustained 
            growth with a net profit of RO5.6 million—a marginal decrease of 
            just under RO200,000 (3.3 per cent) compared with the corresponding 
            period last year. More cheer resulted from the company awarding a 
            dividend payout of RO0.600 per share despite the difficult operating 
            environment it faced. The company’s Annual General Meeting further 
            approved a mandate for the Board to consider and disburse an interim 
            dividend in the course of year 2004.<br>
            <br>
&nbsp;As expected, last year the oil-marketing industry was greatly affected by 
            Oman’s power plant conversion to natural gas from liquid fuels. The 
            switch to gas from diesel may have freed up a lot of Shell 
            <img border="0" src="infor.jpg" align="left" hspace="2" vspace="3" width="164" height="293">Oman’s 
            working capital but as the company held the highest market share for 
            this business segment, the subsequent decline in diesel sales meant 
            it was worst hit. As the gas pipeline project to the south of the 
            country came on stream a little earlier than forecast, the sharp 
            decline in the demand came sooner than expected too. Obviously the 
            lower fuel volumes sold as a result have significantly contributed 
            to a huge decline of RO8.1 million in Shell Oman’s overall revenue 
            in 2003 hovering around RO160 million.<br>
            <br>
&nbsp;“The market shrinkage has had an overall annualised impact of some 250 
            million litres which will only be fully felt in 2004. On the other 
            hand, trade receivables have substantially reduced,” says Dr Andrew 
            Wood, chairman of Shell Oman. Though the impact to the bottom line 
            was fairly small in 2003, finding compensating business has been a 
            tough challenge while keeping the unit costs on fuel at the same 
            levels has proved even more difficult. <br>
            <br>
&nbsp;To make matters worse, high crude prices worldwide caused a steep rise in 
            base oil import prices – the key raw material in blending 
            lubricants. For Shell Oman this was unwelcome news because it runs 
            the only international standard lubricants blending plant in Oman, 
            which is also an ISO certified operation.<br>
            <br>
&nbsp;“With a contribution of the lubricants business to the com-pany’s gross 
            profit of about 12 per cent, this has had a significant effect on 
            the overall results,” adds ronically, as the costs of production 
            rose, the lubricants performance of Shell Oman achieved a new 
            milestone whereby total volumes produced rose to about 53,000 tons 
            during 2003 from 46,500 tons, 80 per cent of which was exported. The 
            company is also very good at selling lubricants to Omani customers 
            and holds around a 40 per cent share domestically.<br>
            <br>
            <b>Low flying aircrafts</b><br>
            In addition, there was a significant drop in international aviation 
            traffic at Seeb International Airport although the aviation fuels 
            business for Shell Oman was given some relief in the early part of 
            2003 courtesy of increased demand by the Royal Air Force of Oman (RAFO) 
            which it served. “For our company we have been able to more then 
            compensate with very high aviation sales in the first half, however 
            these aviation sales were of temporary nature and have discontinued 
            in June 2003 therefore the 2nd half of 2003, our performances were 
            impacted and our overall year performance was at around the same 
            levels as 2002,” explains Irshad Lawati, managing director, Shell 
            Oman in an exclusive interview with OER. “Put simply, we need more 
            flights to refuel at Seeb International Airport.”<br>
            <br>
            <b>Retail rush</b><br>
            Retail fuels had the most positive impact for Shell Oman with a 3.9 
            per cent growth in volumes leading to an all-time high of 812 
            million litres being sold. Five new sites were added to the network 
            in 2003 while the company was relentless in improving its value- 
            roposition and the ongoing improvement of its existing sites. The 
            extensive network of 42 Select convenience stores continues to be a 
            strong business contributor combined with Shell Helix Express and 
            Shell Carwash formats at selected retail sites. Lawati continues: 
            “Our retail coverage is very efficient at the moment. Our main 
            strategy is to develop our sites to gain more volume and to extend 
            service concentration on them to attract more business. The way we 
            develop our propositions is balanced in terms of finding an 
            excellent location and deploying very high standard integrated 
            service elements such as Select stores as well as developing other 
            auto solution facilities.”<br>
            <br>
            <b>People power </b><br>
            In terms of training and development, 2003 saw Shell Oman complete 
            its second year of operation of the Shell Oman Learning Centre that 
            has benefited employees and community members with business and 
            personal training. The company also introduced a special award 
            scheme rewarding staff that performed beyond their normal job 
            requirements. “Training and development is fundamental in sustaining 
            our company’s market leadership and contribution to Oman,” confirms 
            Lawati. Shell Oman has progressed Omanisation to about 80 per cent 
            and, of course, there is now an Omani at the helm of management.<br>
            <br>
            <b>Safety emphasis</b><br>
            While Shell Oman continues to achieve a high standard of financial 
            performance, it is also doing well to satisfy its other corporate 
            goals. 2003 was another good year for safety with the worst injury 
            recorded being a broken finger when one of Shell Oman’s contracting 
            staff was changing a tyre. The company has recently broadened its 
            responsibilities programme to integrate enhanced safety into its 
            operations. The launch of a 7 Pillar programme to safety aimed at 
            developing hauliers across a range of competencies is one example of 
            this plan. During the construction phase of Oman’s investment 
            projects, bulk fuels will be needed and Shell Oman is in a good 
            position to win this business through tendering. “We have got a very 
            good track record in regards to managing the delivery and 
            distribution of fuel products. We have a robust management policy 
            which has kept a high priority on safety while placing more emphasis 
            on health, security and environmental aspects of our operational 
            performance,” insists Lawati.</font></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>The 
            future</b><br>
            Shell Oman’s retail business may continue to show healthy growth but 
            2004 is a challenging year for the company. It will continue to 
            invest in its retail business where it sees steady growth in both 
            volumes and margins. However, pressure will be intense in the 
            commercial and aviation business. Coupled with a contraction in the 
            overall market size for these two classes of business, it is 
            projected that the company will find it difficult to meet the 
            sustained profit performance of the past during the next few years. 
            While other parts of the business grow to augment the gap, 
            shareholders will need to be patient as the company’s management 
            take all necessary steps to alleviate this projected erosion in 
            profit and continue to identify alternative growth sources as well 
            as manage costs at appropriate levels. <br>
&nbsp;It will also be imperative that Shell Oman manages to keep local rivals Al 
            Maha and Oman Oil Marketing Company (OOMC) at bay. Currently, it is 
            still the market leader with the highest average throughput per site 
            of its competitors. The company will need to remain guarded and 
            continue effective branding and communication activities to ensure 
            it retains customer loyalty. Clearly, its commitment to sustainable 
            development from 2003 will continue and its drive as a responsible 
            corporate citizen by extending major support to events such as GCC 
            Traffic Week and the Khareef Festival will continue. If proof was 
            needed, in May 2004 the company has tied-up with the Directorate- 
            General of Tourism in the launch of Oman’s first major domestic 
            tourism initiative: “My Beautiful Oman” to produce and distribute 
            tourism guides via its Select stores and communicate and promote 
            exclusively developed tourism offers across the country.<br>
