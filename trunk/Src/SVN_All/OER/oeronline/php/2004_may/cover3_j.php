            <p align="justify"><font face="Verdana" size="1">Hindsight is always 
            20/20. And Oman Holdings International (OHI) is no&nbsp; exception 
            to the<img border="0" src="beh.jpg" align="right" hspace="5" vspace="2" width="100" height="136"> 
            rule. Thecompany invested a lot of time, energy and resources in 
            2003 for the bidding process of the technical college project, the 
            decision for which, unfortunately, was not in&nbsp; the company’s 
            favour. “Our performance was very good in 2003, and the profits 
            would have been substantially higher if we had won the joint 
            technical college project with the Ministry of Defence with our 
            Australian and Omani partners,” says Behram Divecha, the chief 
            executive of OHI.<br>
            <br>
            “Unfortunately the decision did not go in our favour – so it was a 
            big setback for the company. It not only reduced our profitability 
            substantially, but also brought down our company’s share price and 
            value because the market had expected us to win in view of the 
            absolute appropriateness of our bid. In addition to the money lost, 
            we also lost substantial management and executive time, which is not 
            quantifiable,” he says.<br>
            <br>
            “Apart from that setback, we have done reasonably well,” declares 
            Divecha. In a year that
            <img border="0" src="infor9.jpg" align="left" hspace="5" vspace="2" width="200" height="320">saw 
            the collective revenues of The OER 20 grow by just 1.5 per cent, 
            OHI’s 4.22 per cent jump in revenues despite the major hurdle do say 
            thing or two about the company’s solid foundation. Divecha informs 
            that the company did win a “huge contract in Ghana for laying 
            telephone cables for Ghana Telecom”, has opened a subsidiary company 
            in Qatar which has commenced business recently and is also 
            evaluating opportunities in Sudan. <br>
            <br>
            <b>Management ethos</b><br>
            “In the local market,” claims Divecha, “we continue to work, adding 
            to our activities, agencies and representations, and have reached a 
            stage where we are approached to represent and work with parties 
            rather than we approaching them. These approaches come from 
            recommendations from the many partners we have all over the world. 
            And that is a very satisfying feeling – recognition of the 
            professional approach of our team.”<br>
            <br>
            According to Divecha, OHI is highly diversified and each profit 
            centre is professionally managed. “Authority is decentralised and 
            decision-making is filtered to the most appropriate level. While 
            management places a strong emphasis on information systems, online, 
            real-time reporting keeps the management fully informed of the 
            results and variances from the forecasts and budgets. “One of the 
            most pertinent reasons that staff in OHI are effective and committed 
            is that we allow our staff to learn from their mistakes. Our people 
            know that if an initiative results in a genuine problem, the 
            management will back them.” The attitude and work style has been a 
            hand down of Maqbool Hameed, the chairman and founder of OHI, 
            according to Divecha.<br>
            <br>
            “In the current year, in terms of revenues and profitability, the 
            oil, gas, petrochemicals, power and water segments contributed 
            substantially,” he informs. “A very close second,” according to him, 
            “was the Mariine, IT, Telecoms and Construction subsidiaries. 
            Because of the strong synergy that exists between the operating 
            subsidies, OHI as a company has the ability to survive changes in 
            the business climate and cycles without major ups and downs – it 
            generates revenues and earnings in a steady manner as can be seen 
            from the results since the company went public,” he adds.<br>
            <br>
            <b>The road ahead</b><br>
            “OHI has realised that growth from within is far more difficult to 
            achieve in the current market situation, and we are actively looking 
            at acquisitions within the country and outside,” informs Divecha. 
            “Acquisitions are targeted basically at increasing efficiency and<img border="0" src="cor.jpg" align="right" hspace="5" vspace="2" width="200" height="125"> 
            effectiveness, in addition to making healthy additions to the bottom 
            line” he explains. “So, last year, we purchased the UPS franchise, 
            the Budget franchise and the travel agency which will handle most of 
            the OHI business, and get the earnings of these units, which were 
            earlier going outside the company,” he affirms. <br>
            <br>
            While the earnings from such franchises may not be huge, according 
            to Divecha, they will help the company in creating an effective 
            support system in a way that will allow OHI to offer a complete 
            range of services to its customers and principals. “We do have plans 
            to expand by acquiring more businesses and investing in other areas 
            in the future. Although current plans are being kept quiet, I can 
            only say that you must keep a watch on us,” he says. “Some 
            businesses are developed for long-term growth while some businesses 
            are looked at as short-term businesses – to invest, add value and 
            divest. We continue that strategy,” he hints.<br>
            <br>
            Besides acquisitions, OHI is also looking at collaborations for 
            projects in the international arena. “We are now looking for 
            contracts in markets where international companies work with us for 
            the kind of work that we do in Oman.” Divecha is hopeful that a 
            couple of other contracts that the company is looking at will also 
            come through in this year. “We will continue our efforts&nbsp; to 
            expand our operating territory to Africa and other areas in the 
            region without sacrificing the security of payments,” he informs.&nbsp;
            <br>
            <br>
            <b>Technology and development</b><br>
            “Technology is the cornerstone of increasing the efficiency of each 
            and every aspect of the company, and we continue to upgrade our 
            technology base on a constant basis,” declares Divecha. “We have 
            recently upgraded our computing capabilities – we have increased the 
            capacity of our leased lines with Omantel for our communications 
            systems; we have moved to IP telephony since we represent the best 
            of all IT principals in the country – Microsoft, Oracle, H-P etc.. 
            In terms of other technologies, in oil, gas and petrochemicals, we 
            obtain these technologies and offer them to our principals and make 
            a business out of it as well use them ourselves if possible.”<br>
            <br>
            “With the current advances made in communication, transportation, IT 
            and the impetus that the WTO has created, the whole world is your 
            oyster,” proclaims Divecha. “With the right set-up, you can work 
            anywhere in the world. If large MNCs can do it in a very big way, we 
            can do it in a small way. The important thing is that we have the 
            ability and the reach to work anywhere in the world today. We have 
            done it in the past and we will do so again,” he claims. <br>
            <br>
            “We have MNCs like the Canada-based Enridge and BC Gas (now called 
            Terasen Inc.), which are working with us on the operations and 
            management of the government gas network for Oman Gas Company, and 
            we are looking to working with these two companies on other projects 
            as well,” he informs. “In addition, we have exchanged information on 
            contractors to work with each other. We have recently established 
            operations in Oman for handling oil-based drilling mud for PDO and 
            removing the toxicity from the material before releasing it to the 
            environment. We continue to look out for any opportunity where we 
            manage and others invest, or we invest with others and manage.”</font></p>
