<p align="justify"><font face="Verdana" size="1">
<b>Oman Air, the Sultanate's national carrier, is all set to take a quantum leap 
in its efforts to make its service standards world-class. As part of its ongoing 
customer service campaign, the airline has invited the Singapore-based service 
and partnership guru Ron Kaufman to address its staff and stakeholders to add 
anew dimension to its service initiatives. Vicky Kapur reports</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im4.jpg" align="left" width="144" height="177">Do this 
exercise: Close your eyes and think of Oman Air. Do you see an enervated 
national carrier trying hard to survive in the cut-throat-competitive airlines 
industry? Think again. The last one year has seen the organisation working hard 
to not only improve its efficiency and profitability, but also its service 
standards. With Oman Air's holding company posting operational profits of 
RO586,000 in 2003 as against losses of RO257,000 the year before, things may 
have already started looking better for the Sultanate's national airline. <br>
<br>
Make no mistake - Oman Air has turned the tide in its favour in a year that was 
considered a disaster for the aviation industry what with SARS and the war in 
Iraq all but wiping off profits from the balance-sheets of most air carriers. So 
what is it that Oman Air is doing right that will lead the organisation to its 
desired position of glory in the Gulf skies?<br>
<br>
Improved service, for starters. The airline has employed Richard Coles, a 
specialist in customer service who has 30 years' experience with Qantas, KLM and 
most recently Emirates Airlines. Rick, as he prefers to be called, is a training 
manager specialising in leadership, customer issues, and management skills. He 
has already begun the process of further increasing the professionalism of Oman 
Air's service standards. The aviation company is all set to infuse in its 
front-line staff the energy and enthusiasm necessary to make its service 
standards of global class. And the privately-held airline will now settle for 
nothing less than a complete image makeover in the consumer's psyche. <br>
<br>
As part of the same exercise, Oman Air has invited Ron Kaufman, a 
Singapore-based internationally acclaimed service and partnership innovator and 
author of bestsellers like UP Your Service!, to address a packed event on May 10 
that will have the company's staff and stakeholders (read: frequent fliers and 
top management brass in the country) among the audience. <br>
<br>
&nbsp;Rick adds: &quot;We have many initiatives that will follow Ron's dynamic 
presentation to us and some have already begun. They will address both our 
internal service to each other and our very important service to our external 
customer. We will need to work as a team in order to make every change and 
initiative a success. Ultimately, our customers will benefit.&quot; <br>
<br>
OER presents excerpts from an exclusive interview with the man who has names 
like American Express, Citibank, Compaq, DHL, Hewlett-Packard, IBM, MasterCard, 
Mobil Oil, Motorola, Nokia, Raffles Hotel and Singapore Airlines among his huge 
list of clients. </font></p>

<p align="justify"><font face="Verdana" size="1">
<b>UP Your Service! is a pretty provocative name for a book? Why did you choose 
it?</b><br>
Improving service is not a new topic. But with all the training, campaigns and 
slogans - there are still a lot of people out there who don't get the message. 
(If one of them served YOU this month, you know exactly what I mean.) <br>
<br>
How are we going to reach &quot;the rest of the world&quot; and get this essential message 
through? It isn't going to come from more talking and training. And it isn't 
going to come from a nice book called &quot;How to Deliver Wonderful Service.&quot; That's 
just not bold enough. It isn't shocking enough. It isn't enough to wake the 
dinosaurs from their bureaucratic stupor and provoke them to improve. That's why 
the title is so audacious, to grab attention and pull readers into the book. </font></p>

<p align="justify"><font face="Verdana" size="1">
<b>How are your training sessions different from other trainers? </b><br>
My style of training is to educate and encourage, and also to challenge and 
provoke. I use entertaining stories to get key points across - and then hit hard 
with a direct challenge to create action steps for improvement. I always 
conclude with a connection to the job and to your personal responsibility for 
making things better for your customer, for your organisation, and for you. <br>
<br>
I appreciate good theories, but only teach them when they can be connected to 
practical application. Theory for its own sake is entirely valid in an academic 
environment, but I am much more application focused. I want people and companies 
make noticeable improvements, and feel good about the improvements they make. I 
thrive on getting people involved so my sessions are participatory from the 
beginning to the end.</font></p>

<p align="justify"><font face="Verdana" size="1">
<b>Who benefit more from your presentations - managers or frontline staff?</b><br>
The person who benefits most is the customer! Managers appreciate the examples 
and insights I bring from companies all over the world while the staff finds the 
presentation style refreshing. Both seem to get real value from the chance to 
ask questions and gain a new perspective on how to delight customers, work more 
effectively together and build long-term partnerships into the future.<br>
<br>
<b>Can reading the book replace attending a session?</b><br>
Reading the book gives more time to reflect and consider each of the key 
learning points. It also allows the reader to go back over a topic area at their 
own pace, or move quickly from one related topic to another. Attending a 
session, on the other hand, allows for interaction with the instructor, and with 
your colleagues. If you are keen to develop group action plans, attending a 
session together can be a highly effective catalyst. Of course it isn't really a 
question of which is better or worse. Books and live sessions should support 
each other towards the common objectives of learning, improvement and 
commitment. </font></p>

<p align="justify"><font face="Verdana" size="1">
<b>But do motivational talks really work?</b><br>
Motivational talks can work for generating short-term enthusiasm, but not for 
fundamental education nor for long-term change. Presentations can support 
learning, but may not be enough on their own to bring about a change in attitude 
or any new constructive actions. But if an organisation wants lasting, positive 
change, then books and presentations alone will not &quot;make the difference&quot;. An 
organisational culture must be developed to consistently clarify and reinforce 
desired behaviours. This involves much more than books and talks.</font></p>

<p align="justify"><font face="Verdana" size="1">
<b>Many companies have found that training fails, and are now moving toward 
Human Performance Technology and balance scorecard system - does your book 
address any of these issues?</b><br>
UP Your Service! has a very clear purpose: to help people improve the quality of 
service they provide to their customers and to their internal and external 
service partners. <br>
This book is completely focused on what it takes to make your organisation one 
of the best in the world for quality customer service. </font></p>

<p align="justify"><font face="Verdana" size="1">
<b>How can a company improve their service without paying their staff more? 
Isn't money the key incentive?</b><br>
Money is important. It's a &quot;hygiene&quot; issue that needs to be handled well to keep 
good staff from leaving simply to earn more money. <br>
However, motivation to give customers good service doesn't come only from 
financial rewards. There needs to be personal satisfaction and group 
acknowledgement that accompanies delivering great service. And that is why 
building a service culture is so essential.</font></p>

<p align="justify"><font face="Verdana" size="1">
<b>What is your opinion of the service standards in the Middle East, 
particularly the Sultanate of Oman?</b><br>
Service in the Middle East is incredibly diverse - from outstanding to truly 
horrendous. Standards of service can be impeccable when the culture of a company 
is strong and the commitment to service and service training are clear. 
Excellent examples exist in the manufacturing, retail, hospitality and airline 
sectors.<br>
<br>
In other cases, service seems to be an unknown word - when facilities are in 
poor repair, staff seem more interested in their own social life than any 
'customer', and leadership in service is non-existent. At times the quality of 
service depends on nothing more than the mood of the service provider at the 
moment...which is no guarantee at all!<br>
<br>
This current situation does not take advantage of the natural hospitality that 
is so much a part of Arab culture - as though hospitality is reserved only for 
home or close friends and cannot be extended into the commercial world. I 
believe Arab hospitality could easily become the basis for a service revolution 
in the Middle East - but it will take a very intentional decision by leaders in 
this region to make it so.<br>
<br>
The large expatriate community working in the Middle East bring their own 
cultural styles into the region and the commercial world. This can be an added 
asset in the case of cultures that are naturally kind, gracious and helpful. 
However, without unifying theme, vision and guidance from Arab leadership, this 
mixture of nationalities could devolve into a muddled mess rather than forge 
into a global benchmark for quality service.<br>
<br>
The same is true with government agencies where often times a historical view of 
control and power has prevented a 'customer-oriented' culture from ever taking 
root. This can be changed, but once again, investment in training, building 
service culture and strong service leadership at the top will be required. In 
Oman, you have the special advantage of a unique style of Arab hospitality that 
is among the warmest, kindest and most gently welcoming of all. Leverage that 
with vision, training and strong culture, and you can lead in the region and in 
the world.</font></p>

<p align="justify"><font face="Verdana" size="1">
<b>What are your impressions about Oman Air, and how long do you think will it 
take for it to start providing unmatched service to its customers?</b><br>
Oman Air has terrific facilities, great aircraft, excellent on-board 
entertainment, catering and seats. And the staff are truly committed, kind, 
devoted to their work. The next step is to take this dedication to an even 
higher level of service standards - where 'world class' is not just talked 
about, but understood, admired... and then achieved.<br>
<br>
There is no answer to 'how long will it take'. It can happen on each and every 
flight, in every interaction with passengers; on the ground, in the air, on the 
telephone, over the Internet. Every part of the organisation must be trained, 
motivated and aligned. And this can begin right now.<br>
<br>
Rick Coles will be working with the team at Oman Air to ensure that many of the 
new initiatives are understood by all staff and are implemented with speed and 
consistency. This is so important! A speaker or expert like me can come in and 
provide insight, ideas and motivation, but it takes the team who work day-to-day 
- on the ground, in the air, on the management team - to be sure that the ideas 
become action and the insights are applied.<br>
<br>
With Rick's guidance and everyone on the team working together on every aspect 
of service in every corner of the airline, passengers will experience a service 
that is seamless, truly unique and individualised to meet every customer's 
needs.</font></p>

