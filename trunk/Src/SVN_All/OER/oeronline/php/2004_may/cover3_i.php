            </font><font face="Verdana" size="4">UNDERWRITING SUCCESS</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>A 25 per cent cash 
            dividend over and above of an interim stock dividend of 25 per 
            centsaw Dhofar Insurance’s stock price soar by a staggering 276 per 
            cent during 2003. No wonder it is the only insurance company to be 
            in The OER 20<br>
            </b><br>
            “Dhofar has performed fairly well over the past many years, with the 
            year 2003 being no<img border="0" src="tah.jpg" align="right" hspace="5" vspace="2" width="100" height="137"> 
            exception,” says Taher Taleb Kamel Al Heraki, the chief executive of 
            Dhofar Insurance, the only insurance company to be part of the elite 
            OER 20 rankings this year. “We ended the year with a premium income 
            of RO35.8 million, with a net profit of RO5.274 million,” he 
            proclaims. The net profits displayed a jump of an impressive 19.64 
            per cent over the RO4.409 that the company achieved in 2002. <br>
            <br>
            “As you can see,” continues Al Heraki, “the end results have been 
            very good – we have paid an interim stock dividend of 25 per cent in 
            the last quarter of 2003 and topped it with a cash dividend of 25 
            per cent after the final closings. Patience, dedication and sheer 
            perseverance has helped us get where we are now and, looking back at 
            the 15 years that Dhofar has grown, I am proud to say that we are in 
            a comfortable&nbsp; position today,” he affirms. Little wonder, 
            then, that the company’s share price appreciated 276.11 per cent, 
            from RO1.130 as on December 31, 2002, to RO4.250 as on December 31, 
            2003 – the best among The OER 20.<br>
            <br>
            Al Heraki attributes the company’s consistent
            <img border="0" src="infor8.jpg" align="left" hspace="5" vspace="2" width="200" height="314">performance 
            to the management’s prudent policies as well as the dedication and 
            loyalty of its staff. “We are pleased to learn that we are on par 
            with other leading companies and financial institutions in Oman 
            [being part of The OER 20].&nbsp; We have strived hard over the 
            years, and I feel have rightly earned our place as a market leader. 
            I would attribute our success to prudent underwriting principals, 
            coupled with a very cautious investment policy that has paid its 
            dividends.&nbsp; Last but not least, the very&nbsp; dedicated team 
            that we have are all contributing factors to a successful 
            organization.” <br>
            <br>
            “A&nbsp; cautious, steady growth is always good for any industry, 
            and that it is better to consolidate, and then grow,” believes Al 
            Heraki. “We will continue to consolidate our position as the market 
            leader, be more innovative and expand our operations further.” He 
            adds: “Right from Day 1, we have been careful about keeping our 
            money in safe channels. We have investments in real estate, 
            deposits, shares in the market etc. We think really hard before 
            taking any investment decision, and that has helped to keep us on 
            the safer side. We will never change our investment policy, which is 
            defined by prudence.”<br>
            <br>
            Elaborates Salim Mubarak Al Shanfari, the company’s chairman, in its 
            annual report 2003: “The insurance sector internationally is still 
            characterised with selectivity and reluctancy in accepting risks. 
            This prevailing condition resulted in the re-insurers imposing 
            strict terms, which were not existing earlier when accepting risks 
            from the direct insurers. The heavy losses suffered by the 
            re-insurers due to the aftermath of September 11, 2001, and the 
            catastrophic calamities are the main reasons for their stand.” <br>
            <br>
            The above situation compelled Dhofar Insurance to adopt a new 
            strategy to increase its retention capacity, which enabled it to 
            negotiate competitive terms in the international market. The company 
            increased its paid-up capital by issuing 25 per cent bonus shares to 
            the shareholders twice during the year. “These decisions,” explains 
            Shanfari, “were taken by the company with the intention of building 
            a solid financial foundation aiming to provide better underwriting 
            facilities to retain higher portion of the risks in accordance with 
            the company’s policy to reduce the dependency on the international 
            market.”<br>
            <br>
            <b>The product mix</b><br>
            “We have a balanced premium portfolio,” claims Al Heraki. “We are 
            very cautious in maintaining this balance as our end-results depend 
            on it. The 25 per cent stock dividend paid in the last quarter of 
            2003 has already increased our capital to RO9.375million. A large 
            firm capital base also gives comfort and confidence to international 
            re-insurance companies who look at both the reputation as well as 
            size of the company,” adds Al Heraki.<br>
            <br>
            When queried about the new products/services that the&nbsp; company 
            is planning to launch in the future, Al Heraki said: “We are in the 
            process of adding more products in the medical insurance services as 
            this particular range will soon be much in demand. The life 
            insurance products vis-à-vis individual life coverage; a wider 
            choice of policies will be available for the individual to choose 
            from. We will also be marketing a number of personal lines insurance 
            products for the individual in the future. As the market leader, we 
            are innovative and always eager to mould new covers as and when the 
            situation arises.”<br>
            <br>
            Al Heraki believes that a lot of the local insurance market is 
            hitherto untapped and thus has a lot of potential. “We are not 
            looking outside the country as we think that Oman is not a small 
            market and that the individual life market is yet unexplored. Even 
            the tenders market provides a lot of opportunity. New technology 
            will allow us to tap hitherto untapped sections in the market.”<br>
            <br>
            <b>Future focus</b><br>
            “The company has always endeavoured to spread its activities by 
            opening new branches throughout the Sultanate. We have total of 23 
            branches (at the end of 2003) and are in the process of opening 5 
            new branches in different areas of Sultanate by the end of 2004,” 
            reveals Shanfari. “We have acquired and added to the company’s 
            assets a land measuring 24,456 sq mt with existing buildings at Al 
            Wadi Al Kabir area for which we are presently drawing up a fruitful 
            investment plan. We are also renovating one of the company’s 
            building at MBD Area with the intention of utilising it for better 
            investment,” he adds.<br>
            <br>
            “We will continue with our branch expansion programme, identifying 
            new locations and taking Dhofar Insurance to the remotest corners of 
            the Sultanate. As we increase our presence, so would our personnel,” 
            claims Al Heraki. The company’s strategy is to “introduce more 
            products and market them well.” Says Al Heraki: “We have the 
            infrastructure in place; it will only need a little perseverance. We 
            would also like to rope in ore corporate clientele, as we are better 
            equipped to service them than any other insurer in the market.”<br>
            <br>
            “Technology is changing everyday, and with it come new products and 
            services from the international market. We hope to keep pace with 
            the international market in providing our customers easy access to 
            our products and services through technology. Technology plays a 
            very big role in our business and we are serious about it.” <br>
            <br>
            <b>Omanisation &amp; Training</b><br>
            “We are proud to mention that for the second consecutive year, His 
            Majesty’s Government has honoured us for our Omanisation programme. 
            In this connection, we are pleased to mention that 19 of our Omani 
            staff were sent for training courses abroad and we will continue to 
            train them to qualify to take over higher responsibilities. Our 
            Omani workforce is 71 per cent at the end of 2003 of our total 
            staff,” states Shanfari in the company’s annual statement.<br>
            <br>
            Elaborates Al Heraki: “Currently, we have two ongoing plans for 
            in-house training of our employees, which we hope to complete by 
            July this year. One of the programmes is the laboratory of the 
            language, and the other has to do with the equipment insurance. 
            Then, in September, we will be undertaking courses for Life 
            Insurance and Marketing. Of course, we provide training in phases 
            because, as an organisation, we can’t afford to have everyone 
            attending training programmes at the same time.<br>
            <br>
            “But each one of our employees undergoes training in some or the 
            other form during the year. Last year, for instance, all our 
            employees – right from the frontline staff to managerial personnel – 
            underwent computer training according to their job-requirement.”</font></p>
