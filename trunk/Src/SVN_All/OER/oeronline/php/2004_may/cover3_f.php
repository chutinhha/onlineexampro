            </font><font face="Verdana" size="4">EXPLORING NEW FRONTIERS</font></p>
            <p align="justify"><b><font face="Verdana" size="1">
           
            <font color="#000000">Renaissance Services achieved a significant 
            revenue growth of 58% in 2003, a staggering 307% increase in its 
            share price and a whopping 267% jump in net profits, climbing four 
            steps on The OER 20 ladder – the most impressive leap among the 
            cream- f-the-crop listed companies in Oman</font></font></b><br>
            <br>
            <font color="#000000" face="Verdana" size="1">
            <a style="text-decoration: none">
            <font color="#000000">When OER met him last year, Samir Fancy, the 
            chairman of Renaissance Services, had<img border="0" src="ste.jpg" align="right" hspace="5" vspace="2" width="100" height="135"> 
            declared with poise: “I think 2003 is going to be a year in which 
            we’ll frank the form.” Boy, did they do it and in what style! We’d 
            called 2002 a year of turnaround for the company in our profile of 
            the top 10 companies among The OER 20 last year. In comparison, 2003 
            was a watershed year for it. For proof, look no further than the 
            following highlights: <br>
            • Renaissance Services is No. 1 in terms of revenue change: 58%<br>
            • It has also displayed the highest growth in profits among The OER 
            20: a staggering 267.21%<br>
            • It has managed the highest leap in this year’s OER 20: from No. 10 
            last year to No. 6 this year<br>
            • The stock price of the company, the market indicator of ‘real’ 
            performance, shot up by 307%, from a low of RO1.23 in January 2003 
            to a high of RO5 in December 2003<br>
            • The company managed the highest EPS growth among The OER 20: a 
            whopping 359.38%<br>
            • Renaissance gets top honours among The OER 20 when it comes to 
            Return On Equity: 40.62%<br>
            • The company has aptly rewarded its shareholders with a 30% 
            dividend payout for the year<br>
            <br>
            <b>Ready, steady… grow</b><br>
            So what is it that Renaissance did – or did right – to
            <img border="0" src="infor6.jpg" align="left" hspace="5" vspace="2" width="186" height="300">achieve 
            these laurels in a year that saw collective revenues of The OER 20 
            grow by an anaemic 1.50 per cent? “We were ready,” answers Stephen 
            R. Thomas, the company’s CEO. “2003 was a time when, after years of 
            preparation and building competencies and recent financial 
            restructuring, the company scouted for and met with great 
            opportunities and wasn’t found wanting,” he explains. Or, as he puts 
            it rather pithily, “Preparation plus Opportunity equals Success.” He 
            is quick to add that more than anything else, it is the people of 
            the organisation who have contributed to its enviable growth. 
            “People are the very heart of our business – and they have delivered 
            profitable growth,” he adds. <br>
            <br>
            “We encountered major challenges and significant opportunities, and 
            the company has responded positively to deliver good results, 
            improved shareholder value, and healthy cash flows,” agrees Fancy. 
            The significant opportunities he mentions include those offered by 
            Iraq, a new territory in which the company was quick to establish 
            its presence by</font></a> winning and successfully extending 
            contracts through its Contract Services group. <br>
            <br>
            For the record, Renaissance Services is a diversified company with 
            interests in four core operating businesses: Information &amp; 
            Healthcare Technology (I&amp;HT); Contract Services (CS); Education &amp; 
            Training (E&amp;T); and Media &amp; Communications (M&amp;C). [OER is a 
            publication from United Media Services, part of Renaissance Group’s 
            M&amp;C group.] The CS group performed admirably in the company’s 
            oilfield contractor hospitality business in Oman. The company 
            extended its market reach in the region through the I&amp;HT group 
            businesses in UAE and Yemen; the formation of a new CS division in 
            Kuwait; and specific contract successes for the M&amp;C group in Qatar, 
            and the I&amp;HT group in Qatar, Bahrain and East Africa.<br>
            </font><font face="Verdana" size="1"><br>
            <b>The contracting coup</b><br>
            Like last year, the CS group of the company again proved its mettle 
            and emerged the frontrunner in terms of generating revenues and 
            profits for the company. Not only did its revenues increase by 149 
            per cent from RO12.616 million in 2002 to RO31.386 million in 2003, 
            the profits from operations of Renaissance’s CSG were up 192 per 
            cent from RO3.082 million in 2002 to RO9.014 million last year.&nbsp;
            <br>
            <br>
            A closer look at the company’s CS group reveals that it is, 
            primarily, divided into three segments: Short-term contracts won 
            through cyclical tendering, operated by Tawoos Industrial Services 
            Co. LLC (TISCO) and Rusail Catering &amp; Cleaning Services LLC in Oman; 
            Long-term contracts for BOO (build/ own/operate) and facilities 
            management projects, operated by Renaissance Services’ Permanent 
            Accommodation for Contractors (PAC) Division; and major 
            international projects, including rapid deployment life support 
            projects in sometimes hostile and challenging environments, operated 
            by the company’s Overseas Division. <br>
            <br>
            “The core CSG businesses outperformed 2002 results mainly due to 
            higher than plan occupancy in the PAC projects,” reveals Thomas. 
            “New business initiatives outperformed expectations with the 
            Overseas Division winning new contracts in Iraq, with logistical 
            support from a newly formed division in Kuwait,” he adds. Although 
            the initial contracts won in Iraq are expected to be of a short-term 
            nature, they have opened a whole new world of opportunities for the 
            company, with it expected to pitch for future contracts in its 
            proven areas of strength serving the oil &amp; gas, health, education&nbsp; 
            and industrial sectors in Iraq.<br>
            <br>
            <b>Strength in diversity</b><br>
            Not to be outdone, the company’s other divisions have hadtheir share 
            of success as well. The company’s Education &amp; Training group turned 
            a new leaf in its 2003 operations by posting a RO0.351-million 
            profit compared to a loss of RO0.495 million in 2002. ETG’s revenues 
            increased by 43 percent – from RO1.847 million in 2002 to RO2.650 
            million in 2003. <br>
            <br>
            The Media &amp; Communications group consolidated the positive 
            turnaround it achieved in 2002 and increased its revenues by 16 per 
            cent from RO2.401 million to RO2.797 million in 2003. However, the 
            division’s profits from operations remained low and flat largely due 
            to the time and resources it invested in new products and a new 
            market (Qatar) to lay what Thomas calls “a platform for growth for 
            2004.” In fact, these investments are expected to start adding to 
            the company’s bottom-line from this year onwards. <br>
            <br>
            The company’s I&amp;HT group’s revenues fell by 22 per cent from 
            RO12.053 million in 2002 to RO9.377 million in 2003 compared to a 
            growth of 3.27 per cent in 2002 over its 2001 performance. Thomas 
            attributes the dismal performance of the company’s technology group 
            to “reduction in the overall contract value” at the time the 
            deliverables of a major IT contract were being handed over to the 
            client, coupled with the fact that it is, indeed, a period of 
            transition in the technology businesses. <br>
            <br>
            However, Thomas retains confidence in the potential of the I&amp;HT 
            group: “The downturn in revenue and profit in the Technology Group’s 
            2003 performance interrupts an outstanding unbroken record of 
            sustained growth and high profitability over the past decade. The 
            result should not blind us to the underlying quality, enduring value 
            and potential of this asset.” Fancy, too, expects the group to 
            “bounce back from this downturn; especially as all their regional 
            initiatives showed growth this year.” Thomas is hopeful that the 
            “Technology Group will not repeat the specific contract problem that 
            required special provisioning in 2003.”</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Future scope</b><br>
            The disappointing setback in the I&amp;HT group’s 2003 results 
            notwithstanding, the mood at the company is, expectedly, upbeat. 
            “2003 was a watershed year… This has given us a solid platform for 
            sustainable growth,” maintains Thomas. He also informs that “to 
            seize this opportunity fully,” the company plans to “conduct a 
            strategic review in 2004…to determine how best we may hold the gains 
            that have been made, and turn them into new growth and enduring 
            value for all our stakeholders in the year ahead. We want 
            Renaissance Services to become one of the most admired businesses in 
            the region.” <br>
            <br>
            “Behind these varying results,” avers Fancy, “the businesses have 
            performed well in a variety of challenging environments… The group 
            has shown strength in diversity this year, proving its ability to 
            deliver collective growth in spite of isolated difficulties or 
            downturns encountered in any given business sector.” Concludes 
            Thomas: “The outlook is bright but challenging. 2003 is an 
            exceptional result in the company’s history and the challenge is to 
            sustain that result and build upon it.”</font></p>
