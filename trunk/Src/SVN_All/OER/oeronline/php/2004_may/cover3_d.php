            <b>OMAN AVIATION SERVICES</b></font></font><font size="4" color="#FF0000" face="Verdana"><br>
            </font><font face="Verdana" size="4">FLYING OUT OF TURBULENCE</font></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>For 
            Oman Aviation Services, the airline industry has been as shaky as 
            ever. However, the company’sperformance is fuelling some optimism as 
            its airline division continues to form key alliances, diversify into 
            new markets and reduce costs.<br>
            <br>
            </b><br>
            The anniversary of an airline puts a fresh twist on the well-known 
            adage ‘time flies’. Last<img border="0" src="abdulra.jpg" align="right" hspace="5" vspace="2" width="100" height="138"> 
            year marked a decade in business for Oman Air, the spearhead of Oman 
            Aviation Services (OAS) Co. SAOG. Therefore, it seems particularly 
            appropriate that it should be the airline division that should help 
            steer OAS towards a remarkable recovery in 2003. The company’s 
            operating performance showed a significant improvement, yielding a 
            profit of RO586,000 compared to a loss of RO257,000 in 2002. This 
            news was long awaited. The company had reported three years of 
            continued losses before. “In that sense, our company has registered 
            a turnaround,” says Said Hamdoon Saif Al Harthy, chairman of OAS. 
            “In context of the crisis ridden first half of 2003, the company’s 
            results are more than satisfactory.” <br>
            <br>
            Although very pleasing, the achievement of having
            <img border="0" src="infor4.jpg" align="left" vspace="2" hspace="5" width="200" height="325">profitable 
            operations was slightly dampened by news of an overall net loss 
            (after concession fee and tax adjustment) of RO1.13 million for 
            2003. Still, shareholders needn’t be too disappointed. This figure 
            has improved by 35 per cent from the net loss of RO1.75 million 
            posted in the previous year. So, 2003 was a year of mixed fortunes 
            for OAS but the healthy signs of business revival far outweighed the 
            harsh industry realities such as the 40 per cent in fuel prices 
            during the year adding RO1 million to operating costs. <br>
            <br>
            There are silver linings for the company, believes Abdulrahman 
            Harith Al Busaidy, chief executive officer, OAS. Total revenue 
            increased by RO545,000 (close to 1 per cent) to RO60.5 million. This 
            was enough to lift the company up one place to fourth position in 
            The OER 20. The enhancement of the&nbsp; cash profit from operating 
            activities to RO3.27 million from RO0.74 million should also be 
            considered along with growth of 19 per cent in long-term assets for 
            the company to RO50.2 million which was due to the purchase of an 
            aircraft replacing a leasing arrangement, and therefore lowering 
            lease costs. He says that the depreciations and finance cost of the 
            purchased aircraft was lower than the rentals paid for the leased 
            aircraft. <br>
            <br>
            “The company was successful in securing finance for a Boeing 737-800 
            aircraft on very attractive terms,” explains Al Busaidy. Aside from 
            the cost of RO10.3 million for the Boeing 737-800 aircraft , 
            depreciation at RO623,000 in 2003 was higher by 28 per cent from the 
            previous year. This depreciation reflected OAS’s second aircraft 
            acquisition in July 2003 and the full year impact of the first 
            aircraft bought by the company in July 2002.</font><br>
            <font face="Verdana" size="1"><br>
            “With repayment of the loan at a much faster pace than depreciation, 
            with gradual repayment of these loans each year, the company’s net 
            asset position will significantly improve in the future.“ <br>
            <br>
            Other cost savings have been made. In pursuit of improved 
            efficiencies, the company has implemented a new Oracle-based 
            Enterprise Resource Planning System for finance, purchasing and 
            inventory functions; automated its departure control system at 
            outstations; increased double catering for Oman Air flights from 
            Muscat reducing catering costs overseas; and introduced a training 
            centre for pilots in Oman which has significantly reduced pilot 
            training costs. Sustained monitoring of costs combined with 
            economies of scale through measured expansion on profitable regional 
            Oman Air routes is the path being followed to improve margins in the 
            airline’s business. <br>
            <br>
            Al Harthy reminds us that the air transport industry was still 
            reeling from the aftermath of 9/11 and the global economic downturn 
            in 2003. When it seemed that the situation could not get any worse, 
            the industry was struck with another crisis in the form of the Iraq 
            war which was closely followed by the outbreak of Severe Acute 
            Respiratory Syndrome (SARS). “These developments had their 
            inevitable impact on passenger sentiment... sending airlines into 
            yet another financial crisis.” <br>
            <br>
            Fortunately, the world economy and the industry picked up more 
            quickly than anticipated. The second half of the year showed a 
            remarkable upturn in passenger and cargo travel. Income from 
            scheduled services was RO42.8 million, up 7 per cent compared to 
            2002. Passenger traffic rose by 9 per cent to reach 927,000 persons 
            in 2003. “This was the combined result of a full year operation on 
            new routes that were added to the network in 2002” remarks Al 
            Busaidy. As a result, Muscat has been developed as a strategic hub 
            with greater connectivity and traffic flow across the Indian 
            sub-continent, Middle East and East Africa.<br>
            <br>
            The revenue from air charter services was RO6.1 million, down 
            slightly by RO243,000. This included a one time mobilisation fee of 
            RO231,000. Meantime, the company’s charter service contract where 
            Oman Air provides three ATR 42-500 aircraft to Petroleum Development 
            Oman runs until 2006. <br>
            <br>
            For the airport services activities, handling fees for 2003 
            increased to RO1.2 million from the previous year on the back of 
            increased operations from major airlines, mainly Gulf Air. Total 
            movement at the airport rose to 3.1 million passengers, up 17 per 
            cent, while the cargo tonnage handled was 38,000 metric tonnes.<br>
            <br>
            <b>Pie in the sky</b><br>
            Catering revenue was almost halved to RO3.5 million. As a result of 
            OAS joining forces with Aer Rianta to run Muscat Duty Free, the duty 
            free operation at Seeb International Airport, some RO3.3 million 
            from the duty free business and RO508,000 of business from other 
            terminal outlets at the airport in 2002 shifted from the balance 
            sheet of OAS to the new joint venture company. Nevertheless, the 
            company lessened the cost of catering materials it consumed by close 
            to RO2 million, being the cost of duty free items it used to have to 
            purchase. Moreover, although OAS has withdrawn from the terminal 
            catering business in view of poor profitability, the aircraft 
            catering business for the company grew by 9 per cent with close to 
            2.3 million meals uplifted.<br>
            <br>
            <b>Staff and training</b><br>
            Staff costs increased by RO1 million in 2003, a 6 per cent increase 
            from 2002. In contrast, the company’s employee strength increased 
            marginally to 2,526, a rise of 3 per cent. The increase was 
            restricted to critical operational requirements that would add value 
            to enhanced customer service, productivity and profitability, 
            according to the company. The employment of Omanis at all levels of 
            the company is a major objective. In 2003, OAS reached 76 per cent 
            Omanisation and, more importantly, the percentage is much higher in 
            managerial and high skilled jobs.<br>
            <br>
            <b>The future</b><br>
            This will be the final time that Oman Aviation Services Co. will 
            appear in The OER 20. At an EGM held during 2003, shareholders 
            agreed to change the company name to the airline’s brand name ‘Oman 
            Air’ instead. In late 2003, the government (a 33.8 stake holder in 
            OAS) announced that it would grant the company a long-term soft loan 
            of RO10 million, a move that undeniably demonstrates state 
            confidence in the company’s future. <br>
            <br>
            In 2004 and beyond, passenger and cargo volumes are likely to 
            steadily increase in the near future. The objective of the company 
            is to maintain a cautious but steady approach to achieve 
            profitability. This will be aided by gradually declining insurance 
            premiums which decreased by 12 per cent in 2003 after reaching an 
            all time high following 9/11. <br>
            <br>
            “The outlook for 2004 is quite positive. For airport services 
            activities we expect healthy growth in revenues following the 
            continued increase in operations by all major carriers serving Seeb 
            International Airport. For the airline we expect improved results 
            due to traffic growth, less competition on same routes and an 
            anticipated decline in fuel prices from the second half of 2004.”
            <br>
            <br>
            To counter competition, Oman Air is increasing flights on key 
            routes, forming alliances to protect the home market and expanding 
            revenue opportunities through code sharing on new markets. “The 
            recently announced code share with Gulf Air is an example of this 
            strategy,” notes Al Busaidy. “The company will also continue to 
            invest in new technologies, state-of-the-art management systems and 
            its staff development programme.” This combined with competitive 
            pricing and innovative product development (a great example being 
            the hugely successful ‘The Magic of Muscat’ campaign which saw 
            10,000 holiday packages sold) should help see the airline through 
            any turbulent times ahead. <br>
            <br>
            Overall, the management of OAS is convinced that there is much hope 
            that a brighter skyline is looming. A stronger focus on customer 
            care and innovative product development can help this to materialise 
            producing an increased market share, greater passenger traffic and 
            revenue growth for Oman Air.<br>
