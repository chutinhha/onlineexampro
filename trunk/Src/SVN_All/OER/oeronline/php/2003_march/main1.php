              <p><font face="Verdana" size="1"><b>Oman is taking another step to diversify its economy away from oil production by setting up a polypropylene project in Sohar. Saleh Al Shaibany talks to Dr Mohammed Benayoune, CEO of Oman Polypropylene Project (OPP), and gets an insight on how the project is shaping up</b></font></p>
              <p><font face="Verdana" size="2"><b>Oman Economic Review:</b> Tell us about the latest development at the polypropylene
              plant?<br>
              <b>Dr Mohammed Benayoune:</b> We have invited financial institutions to act as advisors; they are APICORP-Taylor DeJong, ANZ Investment Bank, BNP Paribas, Gulf International Bank (GIB) and Arab Banking Corporation (ABC).<br>
              <br>
              <b>OER:</b> What is the total cost of the project?<br>
              <b>Benayoune:</b> At the moment the figure is around US$200 million, which includes EPC (Engineering Procurement Contract), owner costs, interest during construction, and licensing and financing fees. It also includes start-up costs.&nbsp;<br>
              <br>
              <b>OER:</b> How much money are the banks expected to contribute?<br>
              <b>Benayoune:</b> We are looking at borrowing 70 per cent of the total project cost equivalent to US$140 million. The remaining 30 per cent will be raised through equity participation.&nbsp;<br>
              <br>
              <b>OER:</b> What will be the role of the local banks in raising the
              finance?<br>
              <b>Benayoune:</b> We expect the local banks to play a leading role. They have already expressed their interest and detailed discussions are on with them. We do not expect them to be able to provide the entire project debt since local restrictions prevent them from doing so. The Central Bank of Oman rules that local banks cannot put more than a certain percentage of their total loan portfolio in any one project. They have to split the credit components to various clients.&nbsp;<br>
              <br>
              <b>OER:</b> Which local banks are you holding discussions with?<br>
              <b>Benayoune:</b> We are in very serious discussion with Bank Muscat. We are also in contact with Oman International Bank and National Bank of Oman. Oman Arab Bank is very much interested as well. One or two of these four banks might get together with a regional bank or an international bank to be part of a lead arranging team.&nbsp;<br>
              <br>
              <b>OER:</b> How has the project been structured?<br>
              <b>Benayoune:</b> We brought in ABB and LG in order to get in some expertise in the business part of the project. LG has got the marketing experience and would be off-taking a large part of the exportable portion of the production. ABB has the required technology expertise through Novolen Technology
              Holdings.<br>
              <br>
              <b>OER:</b> Who is going to build the plant?<br>
              <b>Benayoune:</b> ABB and LG are going to build the plant, which is expected to be completed by
              2006.<br>
              <br>
              <b>OER: </b> How confident you are in competing with international producers of
              polypropylene?<br>
              <b>Benayoune:</b> We are confident because we are going to produce a high quality product. We have chosen the technology that has a good reputation in the market. Moreover, we intend to have a good market network through LG International.&nbsp;<br>
              <br>
              <b>OER:</b> Where do you plan to market your product?<br>
              <b>Benayoune:</b> First of all, we need to address the local market. We did a survey on the local market and our study shows that the local consumption by 2006 will be between 40,000 and 60,000 tonnes per year. Basically, we are going to provide the know-how, technical support and backup to local business people who want to invest and set-up downstream industries in polypropylene. Moreover, we are going to organise an awareness campaign among local business community in the form of seminars. These seminars will cover all the various downstream investment opportunities. We intend to work with existing polypropylene consumers and new investors on a win-win basis, and shall provide them with all the required technical support in their own markets. Obviously, the closeness of our plant to our consumers would mean savings in terms of transport costs, insurance and so
              on.<br>
              <br>
              <b>OER:</b> What about the international market?<br>
              <b>Benayoune:</b> About 80 per cent of our product will be heading into the Asian market. It is estimated that by 2006, China will need to import about five million tonnes per year of polypropylene for its domestic market. LG International has an extensive marketing network experience in Asia. The geographical location of LG International, which is based in South Korea, next door to China, is a big advantage since we expect the Chinese to be our biggest customers.&nbsp;<br>
              <br>
              <b>OER:</b> Where are you going to get the raw material from?<br>
              <b>Benayoune:</b> Oman Refinery Company (ORC), will supply the Sohar Refinery with a mixture of crude oil and long residue via a pipeline. The Sohar Refinery will process the feedstock and produce a slate of products including propylene. This propylene will then be used by Oman Polypropylene to produce polypropylene.&nbsp;<br>
              <br>
              <b>OER:</b> In what form you are going to supply your product?<br>
              <b>Benayoune:</b> Our product will be in the form of polypropylene pellets supplied in bags. Our customers will melt down the pellets and put various additives or colouring to make a large variety of products like carpets, bags, diapers, electrical appliances or car components.&nbsp;<br>
              <br>
              <b>OER:</b> When do you expect to start the construction of the
              plant?<br>
              <b>Benayoune:</b> It will all depend on the Sohar Refinery. We expect to start building the plant ten months after the Sohar Refinery's construction has begun since we need less time to build our plant. The refinery is a big project, which will require about 38 months construction time, while the polypropylene plant should take only 28 months, although we may not rush its construction, that is because we want to make sure we do things right.&nbsp;<br>
              <br>
              <b>OER:</b> What is the international price of polypropylene and the averaged fluctuations of prices per
              year?<br>
              <b>Benayoune:</b> The price of polypropylene varies from one region to another. At the moment, polypropylene in Europe costs around US$700 per metric tonne. The price has fluctuated between US$500 and US$800 per metric tonne. The polypropylene market is a cyclical one.&nbsp;<br>
              <br>
              <b>OER:</b>  Who in the region produces polypropylene?<br>
              <b>Benayoune:</b>  Polypropylene is currently being produced in Saudi Arabia, Kuwait, Iran and India.&nbsp;</font></p>
