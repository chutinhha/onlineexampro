                  </font><b><font face="Verdana" size="1">
                  <br>
                    Positive thinking and perseverance are the driving forces 
                    behind Ali Daud, McDonald's licensee for Oman. Nayl D'Souza 
                    meets him and discovers an entrepreneur hungry for yet more 
                    success<br>
                    <br>
                  </font></b><font face="Verdana" size="2">In many ways Ali 
                    Daud's career provides food for thought to many budding 
                    local entrepreneurs. Without some idea of where you want to 
                    go, you can't go far. His decision, aged 21, to eye an 
                    opening at McDonald's has crucially been one of his greatest 
                    given that it helped to spark his sizzling career.<br>
                    Daud, now 36, grew up in many countries. In 1978, after 
                    attending Oman's Hassan Bin Thabit school and the Sultan 
                    School, his parents sent him to study in Switzerland where 
                    he spent five years before graduating. This was a sharp 
                    contrast to learning Bachelor of Arts subjects at New York 
                    University (NYU) where the entertaining distractions of the 
                    world's busiest city formed a backdrop to four years of 
                    serious academic study. <br>
                    It's not surprising to learn that he attributes his business 
                    success to perseverance. Achieving one's goals in life is 
                    rarely accomplished overnight. It takes similar persistence 
                    along with time and hard work. He relented in his pursuit of 
                    bringing the international fast food chain to the Sultanate 
                    after he had graduated from NYU. &quot;I'd kept in touch with 
                    McDonald's. I guess when they became ready, they contacted 
                    me,&quot; he says. <br>
                    McDonald's had planned a major development strategy to 
                    aggressively structure developmental licensees in the GCC 
                    region and was seeking suitable candidates to advance the 
                    brand. The company looked for budding entrepreneurs with 
                    bags of talent, enthusiasm and energy to develop their 
                    international chain of fast-food operations. Following a 
                    competitive interview process, he undertook 
                    on-job-evaluation and was finally chosen by McDonald's as 
                    their sole licensee in Oman. <br>
                    He received one year of intense McDonald's training. The 
                    induction involved months of working as part of the 
                    restaurant crew in England gaining invaluable experience on 
                    the floor. &quot;I was cleaning tables, making hamburgers, and 
                    serving customers,&quot; he recalls. Later, he continued his 
                    training programme in Singapore for six months after which 
                    he successfully led a McDonald's restaurant team in the 
                    Philippines. Many of its employees were also being trained 
                    to work as part of his staff in Oman. <br>
                    Daud's final management induction to McDonald's was to 
                    complete a two-week graduation course at Hamburger 
                    University, McDonald's worldwide management training centre 
                    located in the US. It is designed exclusively to train 
                    personnel employed by McDonald's Corporation or employed by 
                    McDonald's independent owners in the various aspects of the 
                    business to achieve 100 per cent customer satisfaction. 
                    Today, more than 65,000 managers in McDonald's restaurants 
                    have graduated from Hamburger University. <br>
                    For Daud, the year prior to developing the restaurant 
                    business in Oman was a very rewarding experience. 
                    &quot;McDonald's transfers all its know-how to you as a 
                    franchisee of the business. The corporation shares many 
                    basic principals from selecting real estate to marketing, 
                    from managing people to negotiating contracts with 
                    suppliers. It makes sure it is giving its licensees all the 
                    support and knowledge in order for them to be successful in 
                    each country.&quot; <br>
                    The results are plain to see around the city. In March 1994, 
                    Al Daud Restaurants opened McDonald's first restaurant in 
                    Oman. In January 2003, the company opened its fifth 
                    restaurant in Al Khuwair. The company also received 
                    recognition of 'Outstanding Commitment to Customers' and 
                    'Outstanding Results' by McDonald's in the GCC last year. 
                    This is outstanding considering that the local restaurant 
                    business has slumped in the past few years and fast food 
                    sales in the Sultanate have been a roller coaster ride for 
                    many in the market. <br>
                    The key challenge remains to find the right location for the 
                    next McDonald's restaurant. &quot;Growth is dependent on finding 
                    the right locations for new outlets. But there is plenty of 
                    scope for McDonald's to grow in Oman,&quot; he acknowledges. <br>
                    <br>
                    <b>Likeable leadership <br>
                    </b>Describing himself as open-minded, receptive to new 
                    ideas, and a good listener, Daud says he's able to motivate 
                    his workers by creating a healthy working environment 
                    promoting respect and fairness. He is fair, caring and he 
                    leads by example, according to his staff and close friends. 
                    After meeting him, one can't help but vouch for the fact he 
                    seems a regular kind-a-guy.<br>
                    When asked to comment upon the biggest mistake of his 
                    career, Daud recalls no single regret particularly, saying, 
                    &quot;We all make mistakes. Its part and parcel of the learning 
                    curve.&quot; He adds that there's nothing wrong in making a 
                    mistake but its wrong to make one and not learn from it. He 
                    also concedes, impatience is one of his personal flaws. 
                    Sometimes all manner of things could happen quicker. &quot;In 
                    some ways, this is not a weakness. I think everyone needs to 
                    be a little impatient,&quot; he argues. <br>
                    <br>
                    <b>Importance of trust </b><br>
                    Before his work at McDonald's, Daud worked for a small 
                    fashion company in London and also for a family-owned 
                    trading company in Taiwan. For Daud, the Taiwanese people 
                    show exceptional work ethics and take business very 
                    seriously. There he noticed that trust is fragile. Like a 
                    piece of china, once cracked it's never quite the same. Daud 
                    says he has huge admiration for such business principals.
                    <br>
                    &quot;It was a remarkable experience to work in Taiwan. If you 
                    stick to your word and deliver, you gain utmost respect. 
                    Credibility is an important driver of business and therefore 
                    a handshake is worth a lot and I like working in such an 
                    environment,&quot; Daud points out.<br>
                    <br>
                    <b>Tackling challenges <br>
                    </b>Daud says that there have been a few hurdles in his 
                    career that he has needed to overcome. He explains that a 
                    current challenge is posed when trying to get permissions 
                    from government departments that would normally, in other 
                    parts of the world, be something that you would obtain 
                    relatively easily and routinely. <br>
                    &quot;I wish there was an entity that would examine all the 
                    processes and procedures in all the ministries with a view 
                    to simplifying them and making them business-friendly.&quot; He 
                    is certainly not alone in voicing such views within the 
                    business community. &quot;In my experience, a problem doesn't go 
                    away of its own accord. First, it needs to be identified and 
                    quantified before action is taken to address it.&quot;<br>
                    <br>
                    <b>Action man <br>
                    </b>Perhaps, the lesson that Daud best teaches potential 
                    entrepreneurs in the country is that they should devote a 
                    greater part of their time to action rather than reflection. 
                    &quot;It's important to be decisive. If you don't decide you just 
                    linger in the same place.&quot; By all means jot down your 
                    10-point action plans but don't waste time deciding on which 
                    path to take. That way you spend more time posting 
                    accomplishments. <br>
                    As a test of his sure nature, OER confronted him with a 
                    rapid-response question: If you were only allowed three 
                    items to pack in your briefcase on a business trip, what 
                    would these be? &quot;My wallet, my mobile phone and a spare pair 
                    of contact lenses,&quot; came his reply without the slightest 
                    hesitation, and punctuated with a gleeful smirk.
