                    </font><font face="Verdana" size="4" color="#FF0000"><img border="0" src="cover-water-1.jpg" align="left" hspace="5" vspace="5" width="250" height="177">Saving
              the future</font>
              <p><font face="Verdana" size="1"><b>Water shortage in various parts of the country have left many of us wondering if Oman is moving fast enough to avert a crisis. Sue Hutton looks at what is being planned and what can be done</b></font></p>
              <p><font size="2" face="Verdana">His Majesty Sultan Qaboos reiterated the urgent need to regulate consumption of water in order to preserve the economic progress of the Sultanate during his annual meet-the-people tour last month. His message, related to the profound importance of water and the need to find better ways to manage the freshwater ecosystem, is timely given that Oman will convene with other nations at the third World Water Forum in March 2003 to compare what progress is being made to cherish and develop this resource. Incidentally, the Sixth Gulf Water Conference is also being held this month in
              Riyadh.<br>
              His Majesty has already spelt out the dangers of ignoring the water problem, saying, in 1991, "The use of this vital resource throughout the world can have a great impact on future development strategies, and indeed could become a decisive factor in political tension and thus world
              security.<br>
              &quot;Studies indicate that there is, at least, a shortage of over 300 million cubic metres of water each year in Oman between replenishment and demand. With an estimated total population of 2.4 million in 2000 and a renewable recharge to water of one billion cubic metres per year (as estimated by the Food and Agricultural Organisation of the United Nations), just 416 cubic metres of water is available per person each year. This places Oman well within a sector defined as being water scarce. In comparison, the Middle East and North Africa
              (MENA) region as a whole currently has a water share per person of 1,429 cubic metres a year. The most water-short countries in the world are Egypt with 26 cubic metres per capita and the UAE with 61 cubic metres per capita, according to the United Nations Environment
              Programme. Under the business-as-usual scenario, a continuation of current global policies and extrapolation of trends in water management, about four billion (or half of the world's population) would live under high water stress by
              2025.<br>
              In Oman, government officials have described the problem of water shortage as the biggest challenge faced by the country but they have denied that a water crisis is imminent. Earlier this year,
              H.E. Suhail bin Mustahil Shimas, Minister of Housing, Electricity and Water, conceded that many areas in the interior were suffering from water shortage.&nbsp;<br>
              <br>
              <b> New projects<br>
              </b>To counter the problem, a huge water project is in the offing. The plan is to tap ground reservoirs fed by AES Barka SOAC's 20-million-gallons-per-day desalination facility in Barka to ease residents in the Dakhliyah region. The RO15 million to RO20 million (US$39 million to US$52 million) first phase of the project will serve an estimated 42,000 residents. The project includes construction of a pipeline from Seeb to
              Fanja, Bid Bid and Samail, along the Muscat to Nizwa highway. Also as part of the first phase, the contractors will build a ground reservoir at Al Khoudh with a capacity of 1.2 million cubic
              metres. The government had earlier in the year considered the entire project too expensive to develop.&nbsp;<br>
 Although its cost is far less than previous water management projects such as the Al Massarat and Al Sharqiyah water distribution systems, the aforesaid project would benefit more people than both combined. Phases two and three of the project will expand the network in
              Izki, Nizwa, Bahla, Manah and Al Hamra over the coming years. The overall network will be designed to supplement supplies to an estimated 462,000 people over the 25-year design life of the project. Tanker filling stations are also planned en route to supply outlying villages with more water.&nbsp;<br>
 This scheme marks a significant change to the water supply policies in the Sultanate's rural areas that have so far depended on wells, aflaj and small, dedicated desalination units. The very move to distribute desalinated water on a mass scale indicates that renewable freshwater resources cannot sustain the country's water demand.&nbsp;<br>
              <br>
              <b> Freshwater resources<br>
              </b>Located in a hot, arid climatic zone, Oman has average annual rainfall ranging from a few millimetres (mm) in the Najd to 300 mm over the northern mountains. By contrast, the evaporation rate varies from 1,600 mm to over 2,000 mm. This means that water loss in the Sultanate exceeds water
              gain.<br>
              However, domestic consumption is estimated to account for just five per cent of all water demand in Oman. Industrial demand for water is less than five per cent. The greatest consumer of water in Oman is therefore agriculture. It consumes over 90 per cent of renewable freshwater resources and contributes about two per cent to GDP at current prices according to Ministry of National Economy data. On the whole, agricultural water use, in the Gulf region, is very high compared with Russia, the US and UK (which only use around two per cent of freshwater for
              agriculture).<br>
              <br>
              <b>Agricultural water use<br>
              </b>Agricultural water usage has actually exceeded freshwater recharge for many years. The effects of over-pumping water for agriculture are apparent both in Al Batinah and on the Salalah Plain. Groundwater levels have fallen steeply and seawater intrusion of the coastal aquifers is well-established. Coastal date gardens have perished and much small-scale agriculture abandoned. Even if all pumping ceased tomorrow, it would take decades for a freshwater balance to be achieved.&nbsp;<br>
 Experts suggest that if agriculture is to continue efficiently, it should focus on high-quality, high-value crops that require relatively small water inputs closely controlled and monitored by high technology drip irrigation systems. Seasonal crops are preferable. Similar logic can be applied to trees grown for beautification purposes. Effective, municipal re-use of wastewater can be used for landscaping, as well as
              'xeroscaping' - using hardy, native plants adapted to dry conditions.&nbsp;<br>
              <br>
              <b> At what price?<br>
              </b>The single greatest way to develop more conservation is to price water more adequately, says Paul Simon from the US Public Policy Institute. Studies in developing countries suggest that farmers are more likely to respond to agricultural output prices, changes in climatic conditions, land quality and depths of wells from which water has to be pumped rather than increases in the price of water. But appropriate water pricing has been recognised as a key factor in controlling the use of water. The Future Vision Conference - Saudi Arabia, held in October 2002, acknowledged that all consumers should pay a realistic price for their water
              consumption.<br>
              Since it is not possible for a country to produce all its basic foods, a government can take a decision to import it. Importing one tonne of grain can be considered as equivalent to importing the 1,000 tonnes of water needed to grow it. Regional trade agreements can be used to ensure regional food security rather than attempting to reach food self-sufficiency.<br>
              <b><br>
 Are there new aquifers?<br>
              </b>Major aquifers at the Al Masarrat and Sharqiyah regions were studied earlier. Both of these aquifers are now being developed for water supply in the surrounding
              wilayats. The Masarrat scheme has had a contractual cost of RO31.86 million (US$82 million) and will supply 215,000 people by the end of its proposed design life in 2032. Pre-paid cards being used in a pilot water-metering scheme in Muscat are being seriously considered for use in the Al Masarrat scheme in a bid to control water consumption. Al Masarrat was to be built as a BOOT (Build, Own, Operate and Transfer) scheme originally the project has been entirely government
              funded.<br>
              North of Jabal Qara, the groundwaters of the Najd which flow towards Umm as
              Samim, though extensive, are not being recharged, and become progressively more salty towards the northeast. Although the water can, and is being used for agricultural purposes, the activities are unsustainable in the longer term. Once the water has been mined, it will not be
              replaced.<br>
              Several recharge dams have been built across wadis in northern Oman, including one at Sahalnawt near
              Salalah. These dams have had a positive effect in intercepting flash floods, preventing settlements downstream from being deluged. But whether they have actually been cost-effective in increasing water supply by infiltrating groundwater for downstream use as against the cost of their construction is yet to be fully quantified.  Where water would otherwise flow to the sea, there is a clear net benefit; but where dams are sited far inland, they may modify natural recharge patterns on which people have been relying.&nbsp;<br>
              <br>
              <b> Domestic water<br>
              </b> Of greater concern is the availability of water for domestic use. In the rural areas of Oman, the availability of drinking water has been integrated with the supply of water from aflaj and groundwater, while urban areas have received desalinated water for some time. Approximately 40 per cent of Oman's drinking water supply comes from desalination.<br>
  One may be led to believe that water from aflaj is very cheap in comparison with desalinated water, but we also know that natural water levels are declining, which is why one has to dig deeper wells, or deepen aflaj to support agriculture and rural drinking water supplies. At the present rate of use, Oman cannot sustain its natural water resources unless it makes real efforts to conserve
              water.<br>
              Decreases in groundwater availability and deterioration in quality caused by salinisation are now prompting the authorities to consider pumping desalinated water further afield to meet rising domestic demand. Would consumers be paying a price nearer to the actual cost of production? They would be much more careful about using the water for watering gardens, washing cars or even small-scale
              agriculture.<br>
              The ministry has announced schemes lately to build water distribution networks and reservoirs around the country. Aside from the project to supply free water from 33 wells to the residents of
              Samail, Bid Bid and Izki, RO3 million (US$7.8 million) is being spent on a scheme to supply Al
              Amerat, and RO6.1 million (US$15.86 million) on water supply networks for Al Maabelah and Al
              Mawaleh.<br>
              <br>
              <b>Industrial water</b><br>
              Industrial processes may have a much higher added value than agriculture (e.g. petrochemicals). Some industrial processes use a lot of water. If the cost of water exceeds the value of a product, there is little point in investing towards its manufacture.<br>
 Muscat Municipality, which charges two baisas per litre of water, is aiming to foster better resource management. A few blue chip companies in Oman already practice this in the city.&nbsp;<br>
 All car washes in Oman waste water. Shell car wash operations in the Muscat use 50 gallons of water per car. Of this amount, 35 gallons is recycled water and 15 gallons is freshwater, which is used in the final rinse.&nbsp;<br>
 Said Al Harthy of Shell Oman Marketing SAOG, says, "To get total cleanliness in the final rinse, we don't use treated water." A local company, Al Bina Al Mumtaz Contracting Company
              LLC, recently announced plans to introduce a total recycling system for treating water used in car washes. A spokesman from the company estimates that more than 35,000 gallons of water are used in car wash facilities in the country each day.<br>
              <br>
              <b>   Is desalination the answer?<br>
              </b>The good news is that the cost of desalinisation is falling. The bad news is that the price is never likely to be low enough to cost-effectively supply agricultural needs. It is estimated that US$73 billion will be needed for desalination projects in the Gulf up to 2030. Financing for desalination plants in the GCC countries has mainly been provided from state budgets and partly from foreign loans; but private sector participation will be vital in the future. With the changing strategies for development, and reduced government funding, there has been an increased dependence on securing external funds particularly from development agencies such as the World
              Bank.<br>
              Desalination may not the best solution for increasing water supply in all cases. The cost of desalinating water depends on the capacity and type of plants. Unit costs are likely to be higher in Oman than in industrialised countries because of the scale of production, maintenance, and the need to import technology and
              labour, among other factors.<br>
              But desalination is fast becoming the major option for water supply in arid countries. Still, the cost of energy required to process salty water is a fundamental issue. Where energy supplies such as oil and gas are cheap and plentiful, desalination costs will be cheaper. In the longer term, when energy resources become scarce, the cost of desalination becomes more daunting, and realistic attempts must be made to recover the costs from
              consumers.<br>
              Over the past decade, The Middle East Desalination Research Center
              (MEDCR) based in Muscat has invested heavily in regional training by conducting short courses and sponsoring students and professors involved in research projects. Shannon McCarthy, MEDCR's associate director for development, has previously emphasised that training locals to operate desalination plants is another future challenge. She says,
              "A minimum of 50,000 additional technical experts of various professional levels would be needed to service the desalination industry in the region.&quot;<br>
  Oman is more fortunate than other GCC countries since it has marginally higher rainfall, which does recharge shallow aquifers on both sides of the northern Oman mountains and the Salalah plain from Jabal
              Qara. The availability of groundwater has, in large part, staved off the transition to desalinated water supply, as seen in other GCC
              states.<br>
              The cost of producing desalinated water is high, but cost of recovery has two components:</font></p>
              <ul>
                <li><font size="2" face="Verdana"> It makes economic processes more sustainable rather than committing non-renewable energy resources to non-sustainable water production</font></li>
                <li><font size="2" face="Verdana"> It increases awareness of the need for water conservation among
                  consumers</font></li>
              </ul>
              <p><font size="2" face="Verdana">Local research into desalination techniques and the establishment of a local desalination industry, rather than relying on outside suppliers should make the cost of desalination cheaper, says Dr. Jamil Al
              Alawi, member of the World Water Council.<br>
              Much greater use can, and should be made of treated recycled wastewater for municipal and garden irrigation, certain industrial processes, and even protecting existing aquifers from seawater intrusion. Aquifer protection policies will ultimately provide alternative water reserves, which can be used initially as emergency supplies for domestic and commercial use, and in the longer term, become a significant
              component.<br>
              <br>
              <b>Water management<br>
              </b>As water crisis looms, some key questions have to be answered and a strategic water policy adhered to. His Majesty's call that every effort should be made to develop a policy of rationalisation of water may require the integration of responsibilities for water into one strategic planning and policy think tank.&nbsp;<br>
  "Many ministries are involved in the development of a national plan for water resources to strike a balance between availability and requirement. This may, in part, be an obstacle to identifying, developing and implementing a unified water management system," says a local water industry consultant who sought
              anonymity.<br>
              Such an institution would have the legislative authority to recommend and implement water demand management strategies. At present, several government organisations have disparate responsibilities for water (as can be seen in the box above). Sometimes these tasks appear to clash. Rather than challenging the conflicts in the interests of conserving water for future use, the ministries can sometimes hold a position which most closely coincides with their own targets and  policies. Certainly, it would seem that no single body has responsibility for implementing water demand management or water quality management policy.  Such fragmentation, it could be argued, makes it much more difficult to implement an overall national strategy for water
              development.<br>
              <br>
              <b>Outlook<br>
              </b>In the short term, building desalination plants would be necessary to meet the increasing water needs of Oman's growing population; but for the long-term, Oman must ask if this policy is sustainable. Dr. Abd Al Karim Sadik of the Kuwait Fund for Arab Economic Development, says that in order to encourage private sector participation in the construction of desalination plants and other alternatives, and to secure additional funding, the government must raise the (low) retail price of water despite the political, social and economic repercussions involved.<br>
 Moreover, repeated calls for rational water use in the GCC emphasise the need for accurate data and information. Routine measurements, which have to be assessed for reliability and depend on accurately calibrated equipment, are not a high priority, yet such data is vital for planning purposes. Only by repeatedly measuring water levels, can one hope to begin to see any sort of pattern or cycle, should any exist, or discern underlying trends. Today, Oman is certain about is finite water resources. This means water shortages in Oman can be predicted more efficiently in the future in order to explore various water demand management options and to implement appropriate water pricing schedules.</font></p>
