                      dams as one of Oman's water management solutions<br>
                      </font></b><font size="1" face="Verdana"><br>
                      There is uncertainty in the water industry as to the
                      levels of renewal recharge dams can bring. Some experts
                      argue that they are more cost effective than desalination
                      plants while others feel that there is a lack of evidence
                      to demonstrate to what extent such schemes store water.
                      Finding the type of water storage that will provide the
                      maximum benefits is going to be critical if communities
                      intend to adopt and progress a renewable resource
                      strategy. But maintaining groundwater levels in key
                      aquifers is not easy. In Oman, the government has made
                      very large investments to construct recharge dams in an
                      attempt to protect water supplies and protect the
                      ecosystem. As the debate continues, Ali Al Abry fields
                      OER's questions:<br>
                      <br>
                      <b>Oman Economic Review:</b> Why do we need recharge dams
                      and can you briefly explain how they operate?<br>
                      <b>Ali Al Abry:</b> The purpose of a recharge dam here in
                      Oman is to enhance renewal of the groundwater resources
                      for agricultural use in target areas downstream of the
                      dam. These dams store flash flood water that is otherwise
                      lost to sea or to the desert. After a few days, when the
                      silt of the flood settles, clean water is discharged into
                      the designated recharge channels through controlled valves
                      in such a manner that all the stored water is recharged to
                      the target areas through these channels.<br>
                      <br>
                      <b>OER:</b> How many recharge dams are there in
                      Oman?&nbsp;<b><br>
                      Abry:</b> There are 18 recharge dams here.<br>
                      <br>
                      <b>OER:</b> Where is the biggest recharge dam in the
                      country and approximately how much water can it contain?<br>
                      <b>Abry:</b> The biggest recharge dam in terms of the
                      reservoir is on Wadi Al Khawd, Seeb. It can store up to
                      11.60 cubic metres of water.<br>
                      <br>
                      <b>OER:</b> Are any dams being built?<br>
                      <b>Abry:</b> Currently no dam construction is in progress.
                      But it is expected that four recharge dams at Wadi Bani
                      Kharus near Wilayat Musnnah, Wadi Sarooj in Wilayat of
                      Madhah, Wadi Sahna, Wilyat Musnnah, Wadi Sarooj in Wilyat
                      of Madhah, and Wadi Mistal Wilayat Nakhl will commence
                      construction in mid-2003 and be completed by the end of
                      2004.<br>
                      <br>
                      <b>OER:</b> Approximately how much investment has been
                      poured into dams?<br>
                      <b>Abry:</b> To date, about RO40 million (US$104 million)
                      has been spent on the study, design and construction of
                      these dams.<br>
                      <br>
                      <b>OER:</b> Are we sure that these dams actually work
                      effectively?<br>
                      <b>Abry:</b> The government is beyond any doubt that these
                      recharge dams work very well. This has been supported by
                      locals living in the downstream of the recharge dam
                      (target areas) who have benefited by the recharged water
                      from the dam as well as by various international
                      consultants during their feasibility studies.<br>
                      <br>
                      <b>OER:</b> How is their progress being monitored? Is
                      evidence being collected to check their progress in
                      recharging aquifers?<br>
                      <b>Abry:</b> The government has engaged international
                      consultants time to time, to evaluate the efficiency of
                      the recharge dams as well as in-house studies on a regular
                      basis, which confirmed that dams are contributing a lot in
                      enhancing the water situation in the targeted areas.<br>
                      <br>
                      <b>OER:</b> Are such dams value for money?<br>
                      <b>Abry:</b> Since it is clear and confirmed that dams
                      work well for the purpose they are built, they are value
                      for money. For example, between the time during the first
                      recharge dam (being built in 1985) and today, about 538
                      million cubic metres of water has been stored and
                      recharged to the aquifer through all the dams in Oman.
                      Economists have worked out that recharge water through
                      dams costs about 80 baisas per cubic meter compared to
                      desalination water, which costs 445 baisas per cubic metre.
                      This is probably the reason that the Ministry is flooded
                      with citizens' requests to build such dams in their areas.<br>
                      Coupled with other benefits such as halting the spread of
                      seawater intrusion and protecting downstream villages and
                      roads from floods, the construction of recharge dams
                      offers many attractive benefits. Not least of these could
                      be the security it offers to downstream communities to
                      sustain and adapt their lifestyle to changing social and
                      economic conditions, while allowing the population to plan
                      for new horizons and opportunities.<br>
                      The performance of recharge dams does depend on the
                      characteristics of the climate, so that should there be
                      drought, there will be little or no surface water flow to
                      provide recharge because there is no rainfall. Estimating
                      cost-effectiveness does, therefore, depend on the nature
                      of the climate.&nbsp;<br>
