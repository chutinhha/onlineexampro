            <p align="justify"><font face="Verdana" size="1">The history of 
            Oman’s Media sector dates back to the very beginning of His 
            Majesty’s reign in 1970. That can be termed as the dawn of the era 
            of modern media in Oman. We are proud that Media was the first 
            sector to achieve a milestone after Renaissance – Oman Radio was 
            launched within a week of His Majesty acceding to the throne. A year 
            later, Al Watan, the country’s first newspaper, was launched. Then, 
            the rest of the papers followed – Oman, Times of Oman, and Oman 
            Observer etc. As things stand now, we have about 40 newspapers, 
            magazines and periodicals in the Sultanate. <br>
            <br>
            A lot of development has also taken place in the field of radio and 
            TV programming and broadcasting. We are now broadcasting in the 
            entire world. We have our studios in Muscat and Salalah, and crews 
            in the various wilayats of the Sultanate, which cover each and every 
            activity that deserves to be covered – be it economic, social, or 
            political. <br>
            <br>
            <b>Key developments</b><br>
            Recently, His Majesty issued a Royal Decree promulgating a law 
            allowing private players to own and operate radio and TV stations in 
            Oman. That is a big evolutionary step forward for Omani media. The 
            response of the private players has been good, although I would have 
            to admit that it hasn’t been up to our expectations. We have 
            received several applications, mainly for FM, and we are hopeful of 
            issuing licences in the near future.<br>
            <br>
            Last year, another major development was the amendment made to the 
            Publications Law, easing the norms governing the issuance of 
            licences for publishing dailies in the Sultanate. In the past, the 
            law required the applicant company to be a listed company. The case 
            now is that even a closed company can own and publish a daily 
            newspaper in the country. Such flexible procedures for private 
            participation are intended to provide a boost to the sector as a 
            whole.<br>
            <br>
            <b>Strengths &amp; weaknesses</b><br>
            Our strengths are based on our values, which are in turn extracted 
            from our society’s principles, beliefs, and aspirations. Our work in 
            the sector is based on our conviction that we have a sense of 
            collective responsibility towards our country’s history, its 
            present, and its future. Our strength stems from the ground that we 
            stand on – our identity, culture, values, and ethics. But that 
            doesn’t mean that we isolate ourselves. While we accord priority to 
            projecting Oman, our windows are open for the world. <br>
            <br>
            As I see it, our primary weakness lies in the perception that others 
            have of us, based on other regional media. People have to understand 
            that the goals and aspirations of Omani media are totally different 
            from those of some of the other media in the region. In the past, we 
            have been evaluated based on others’ actions, which is unfair. While 
            we respect others’ goals and aspirations, we should not be judged on 
            their performance. Our goals are to serve our case, which is to be 
            of assistance in the comprehensive development process of the 
            Sultanate.<br>
            <br>
            <b>Future challenges</b><br>
            There are a lot of challenges ahead – we have to keep abreast with 
            the technological developments as well as keep pace with the growing 
            competition. In the media sector, it is the case of open skies. And 
            the fact that we are not alone in the field is a healthy thing for 
            us. Competition keeps the machine running and improving. We want to 
            learn from our shortcomings and want to tackle them.<br>
            <br>
            Private participation is increasing mainly in the print media – the 
            government owns only two dailies while the rest belong to the 
            private sector. I think the private sector should now introduce 
            itself strongly in the production of TV and radio programming. 
            Indigenous production of Omani programming will enhance, improve and 
            boost Omani media’s performance as a whole. We’ll not hesitate to 
            provide the encouragement required from the government’s side.<br>
            <b><br>
            New technologies</b><br>
            We welcome the emergence of new technologies such as the Internet 
            and mobile messaging as they help in spreading knowledge and 
            enhancing people’s awareness. These technologies help deliver news, 
            facts, and knowledge to every individual, anytime. Information flow 
            helps in the creation of a better society with productive people. 
            These are tools of knowledge. Successful firms and ministries of the 
            future will be those that can make maximum use of these tools. <br>
            <br>
            <b>Foreign media</b><br>
            The impact of the decision allowing the airing of international news 
            channels in Oman has been greatly positive. Competition has 
            intensified in Omani airwaves, and it is good for the customers that 
            they have a wide choice. On the other hand, it is also good for us 
            because it helps us in improving our service. Right now, according 
            to law, only Omani-owned firms can own and run media houses in Oman. 
            However, it is in the same law that these firms can bring in 
            internationally produced programming, and relay it here in Oman. So, 
            practically, outside firms can have a stake in Omani airwaves.<br>
            <br>
            <b>Room for more?</b><br>
            Whether or not there is room for more publications in Oman depends 
            on the visibility studies that need to be conducted by those 
            interested in launching new media vehicles in the country. I think 
            there is always room for more newspapers and magazines, particularly 
            specialised ones. It is time for quality rather than quantity. 
            Quality always has a market, and people are ready to pay for it. It 
            is a matter of enrichment – enrich your case by quality. But do it 
            only if you can distinguish yourself from the rest of the clutter.<br>
            <br>
            <b>The bottom line</b><br>
            The Press has a very dangerous duty to perform, because what you put 
            in the media goes into people’s hearts and minds. The Press create 
            ideas, which determine people’s actions. It is our duty to provide 
            the readers and audience with facts and constructive criticism, 
            which should not be based on personal attitudes. Healthy, unbiased 
            public opinion is the hallmark of an excellent Press, which will 
            guide people towards the right participation in nation building.</font></p>
