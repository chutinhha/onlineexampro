<p align="justify"><font face="Verdana" size="1"><br>
</font><font size="4" color="#FF0000"><font face="Verdana">The virtual 
corporation<br>
</font></font><font face="Verdana" size="1"><br>
<b>Organisations are not only buying and aligning themselves with others in the 
same industry sector, but they are crossing industrial boundaries and 
geographies as well. Are ‘virtual corporations’ really what the future holds for 
the world of business?</b><br>
<br>
<img border="0" src="im18.jpg" align="left" hspace="6" width="200" height="187">It 
is almost impossible these days to pick up a newspaper that doesn’t contain news 
of a merger, acquisition or joint alliance. Companies are getting it together to 
ensure that they have all the skills and competencies they need to compete 
successfully.<br>
<br>
Not only are firms buying or aligning themselves with others in the same 
industry sector, but they are crossing industrial boundaries and geographies as 
well. These moves are blurring and reshaping the very structure and organisation 
of corporations in unprecedented ways.<br>
<br>
Reportedly, strategic alliances where resources, knowledge and expertise are 
shared among a consortium of organisations are the competitive weapon for the 
future. Indeed, in his book The Trillion-Dollar Enterprise, Cyrus Freidheim, 
vice-chairman of Booz Allen &amp; Hamilton, asserts that, in the future, just a few 
dominant enterprises will control up to two-thirds of the global market in their 
respective industries.<br>
<br>
Rather than global organisations, these superior players will be ‘‘vast networks 
of corporate alliances – coalitions of independent firms acting in concert as 
single companies’’.<br>
<br>
He describes this phenomenon as a ‘‘new form of economic concentration [that] 
will capture the best capabilities of its partners, yet avoid the bureaucratic 
disadvantages of size. It will operate on a world-wide scale, with enormous size 
and power,’’ and he proclaims, ‘‘It will be the trillion-dollar enterprise’’.
<br>
<br>
But are these ‘‘virtual corporations’’ as they have come to be known, really 
what the future holds for the world of business? <br>
<br>
They are, of course, one possible scenario. There would be many advantages not 
only for those involved in the alliance but also the customers, in terms of 
lower prices, rapid delivery and much more. All the benefits that can be gained 
in bringing together the crème de la crème of their particular field – be it 
logistics, manufacturing, services, etc. <br>
<br>
Indeed, examples of such networks already exist in the marketplace. Freidheim 
points to the commercial aviation industry where ‘‘hundreds of companies, from 
parts manufacturers to airlines, have formed two networks—the Boeing and Airbus 
alliances—that function as single organisations and compete for global 
dominance’’. <br>
<br>
But there are disadvantages to these ‘‘virtual corporations’’ too. Surely, if 
the best-in-class suppliers were bound into a strategic alliance and those that 
don’t quite make the grade dropped, the best of the best would eventually be 
inundated with work. As a result, they may lack the resources to carry out that 
work which could lead to a lack of quality capacity, which in turn could mean 
prohibitively high prices being attached to the use of such suppliers. <br>
<br>
Another concern is that an organisation may disentangle its value chain to 
produce ‘‘independent companies’’ that focus on their particular core 
competence. These separate elements, acting alone would then potentially begin 
to add back overheads. For example, the logistics department may decide that in 
order to grow, it needs a finance or purchasing department of its own. These 
once independent companies would become integrated and new inefficiencies would 
be born. <br>
<br>
The total virtual corporation is still a long way off. For the time being, at 
least, much of this kind of activity is confined to joint ventures where 
organisations remain integrated yet ally themselves with strategic partners. 
These alliances allow them to trade core competencies to mutual advantage as 
appropriate.<br>
<br>
The five keys to successful “virtual management” <br>
• Marry well – choose the right partners for the right reasons. <br>
• Play fair – ‘win-win’ opportunities at each link. <br>
• Offer the best and the brightest – invent human resources in the relationship.
<br>
• Define objectives – each partner company should know its expected payback. <br>
• Build a common infrastructure – <br>
communication is key. </font></p>

