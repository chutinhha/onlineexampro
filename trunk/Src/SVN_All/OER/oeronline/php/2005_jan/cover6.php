            <p align="justify"><font face="Verdana" size="1">The evolution 
            process of the development of the manufacturing sector in Oman 
            started from the first Five Year Plan period (1976-1980), when the 
            priorities were established and policies formulated to initiate 
            industrialisation in the country. During the initial years, 
            priorities were given in setting up small- and medium-scale 
            industries to manufacture food and construction related products 
            aimed at import substitution. This was the period during which the 
            government formulated several incentive schemes for assisting the 
            manufacturing sector. <br>
            <br>
            Various schemes like government soft loan, tariff protection against 
            import, Customs duty exemption, tax holidays etc. became effectively 
            operational during the second Five Year Plan period (1981-85). 
            During the second plan period, the first phase of a fully serviced 
            industrial estate at Rusayl was constructed. This was followed by 
            the development of four other industrial estates in different 
            regions of the Sultanate to provide the basic infrastructure. As a 
            result, several small and medium industries were set up in various 
            parts of Oman to cater both local needs and regional export markets.
            <br>
            <br>
            The contribution of manufacturing sector to GDP increased from a 
            mere RO2 million in 1976 to RO442 million in 2002. This growth was 
            achieved as a result of the continuous efforts on diversification, 
            commercial exploitation of gas resource, adoption of new investment 
            policies, revamped government soft loan schemes, attractive 
            incentives packages and the establishment of fully serviced 
            industrial estates and other infrastructure facilities in various 
            parts of the country.<br>
            <br>
            The sixth Five Year Development (2001-05) plan aims at manufacturing 
            sector contribution to GDP grow to RO712 million by 2005. The plan 
            also proposes to achieve an investment of RO1,205 million in mega 
            industrial projects through the public and private sector 
            partnership. <br>
            <br>
            However, the beginning of sixth Five Year Plan period has witnessed 
            serious political and economic turmoil globally and in the region 
            particularly. The period also witnessed continuous decline in global 
            foreign direct investment (FDI). There was a sharp decline of 41 per 
            cent in global FDI during the year 2001 and the declining trend 
            continued in 2002 and 2003 also. In spite of highly unfavourable 
            global economic and regional political conditions, Oman could 
            progress well towards attracting large industrial sector investment 
            to the country. <br>
            <br>
            The discovery and commercial exploitation of natural gas and its 
            immense potential as the source of energy and feedstock for the 
            manufacture of several value-added industrial products has changed 
            the dimensions of growth potential and diversification of the 
            manufacturing sector in Oman. The total investment in gas-based 
            industry sector projects under construction is estimated at RO1,300 
            million. This includes Oman-India fertiliser project, polypropylene, 
            petroleum refinery and third train of LNG. Other gas-based projects 
            under various stages of development are the fertiliser project at 
            Sohar, methanol, polyethylene, aluminium, steel and ethylene 
            dichloride. The estimated investment in these projects would be 
            around another RO2,100 million. <br>
            <br>
            These projects are expected to go on stream during at the various 
            periods of 2006-08. From the year 2006-07 onwards, the country 
            should begin to reap the rewards of current investments in the mega 
            manufacturing sector projects. The sector’s contribution to GDP will 
            witness remarkable growth, the exports will grow multifold and both 
            direct and indirect employment opportunities will increase.<br>
            <br>
            World technology leaders like Dow Chemicals, Alcan, LG 
            International, Ferrostaal and major companies like Kribhco, Iffco 
            etc. are partnering as joint venture partners with various mega 
            industrial projects in Oman and bringing in investment and latest 
            technology in their respective field to the country. This is the 
            clear recognition of confidence in Oman by the international 
            investors as an investor friendly and attractive destination for 
            investment in the region. <br>
            <br>
            Once these mega projects are implemented as planned, it will pave 
            way for innumerable investment opportunities in downstream and 
            ancillary projects, specifically in petrochemicals, metals etc. 
            Additional logistics and infrastructure requirement to support these 
            projects would be huge. There will also be substantial additional 
            investments in these areas.<br>
            <br>
            World-class infrastructure facilities, incentives packages, 
            attractive corporate tax and tax holidays, soft loans, 
            simplification of ‘doing business procedures’ and transparent 
            corporate governance has made Oman as an attractive destination for 
            investment. The government has been constantly reviewing the laws 
            and further simplifying the procedures. The setting up of proposed 
            one-stop-shop and creation of Web-based transaction facilities of 
            all ministry related procedures and the changes being contemplated 
            in the Foreign Capital Investment Law etc. will further add the 
            attractiveness and easiness to invest in Oman. <br>
            <br>
            Establishment of Knowledge Oasis Muscat (KOM), a knowledge-based 
            state-of-the-art information technology park at Rusayl, is an 
            important milestone in attracting investment in the IT sector, the 
            fastest developing sector in the world. KOM has attracted a number 
            of investments in IT and IT-enabled services. Two IT colleges are 
            also established at KOM, which also focuses on supporting small IT 
            firms and entrepreneurs who are provided with incubator facilities.<br>
            <br>
            A recent major milestone and a step forward in attracting foreign 
            investment and technology and also boosting the trade is the TIFA 
            between Oman and the US. TIFA is the stepping-stone towards FTA with 
            the US. FTA will encourage foreign direct investment to Oman 
            especially from the US and will open up duty-free export of Omani 
            products to the US. <br>
            <br>
            To sum up, it is important to know how the world community rates 
            Oman as an investment destination. The Fraser Institute’s ‘Economic 
            Freedom of the World 2004’ has placed Oman at 18th position among 
            the 123 countries surveyed. The latest ‘Corruption Perception Index 
            (CPI) 2004’ of Transparency International has ranked Oman at 29th 
            position among 145 countries. Oman’s CPI ranking is the best among 
            Arab countries. This is clear testimony to the achievements and how 
            the world community perceives Oman as an attractive and safe 
            destination for investment.</font></p>
