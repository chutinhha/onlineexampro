                    <font face="Verdana" size="1"><b>Ociped, EIP of Jordan sign 
                    MoU </b><br>
                    The Omani Centre for Investment Promotion &amp; Export 
                    Development (Ociped) and the Establishment for Investment 
                    Promotion (EIP) of Jordan signed a Memorandum of 
                    Understanding (MoU) on mutual investment promotion between 
                    the two countries. The MoU was signed by the executive 
                    president of Ociped, Eng. Salem bin Nasser Al Ismaili, and 
                    by the director-general of EIP, Reem Badran in Amman.<br>
                    <br>
                    The MoU aims at exchanging investment regulations, 
                    periodicals, print-outs, studies and research and details of 
                    investment opportunities available in Oman and Jordan, said 
                    Eng. Al Ismaili. “Under the MoU, a joint committee will be 
                    formed to encourage investment between the two countries and 
                    would be chaired by the director-general of EIP in Jordan 
                    and Ociped executive president,” he said. <br>
                    <br>
                    The two sides hope that the MoU will further strengthen the 
                    growing relationship between Oman and Jordan. It will give 
                    an impetus to economic, investment and trade cooperation 
                    between the two countries. The committee will meet annually 
                    or whenever it is required. There will be regular exchange 
                    of visits from both sides and joint investment seminars will 
                    be held.</font></p>

