                    Al Wahaibi, the CEO of OOC. It is also OOC’s first 
                    investment in the petrochemical sector outside Oman. OOC is 
                    hopeful about the future prospects of TOC. TOC seems well 
                    positioned to grow in the future in Thailand and other 
                    countries, especially the neighbouring Asian nations, said 
                    Al Wahaibi. This will help OOC expand its business in the 
                    Asia Pacific region. <br>
                    <br>
                    TOC, established in 1990, is a joint venture between various 
                    leading petrochemical companies in Thailand. Upstream 
                    petrochemical used in producing plastic resins is one of the 
                    most important products of the company. It is used in 
                    consumer products, construction and manufacturing 
                    industries. Besides, the company is investing approximately 
                    $210m in an ethylene glycol (EG) plant, which is expected to 
                    commence operations in the second quarter of 2006.

