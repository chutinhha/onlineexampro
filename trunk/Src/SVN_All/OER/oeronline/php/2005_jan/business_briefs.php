                    <font face="Verdana" size="1"><b>Banks’ assets cross $12 
                    billion</b><br>
                    The aggregate assets of commercial banks in Oman crossed the 
                    $12-billion-mark at over $12.09 billion (RO4.649 million). 
                    According to provisional statistics the combined assets of 
                    banks increased by nearly 4 per cent from $11.65 billion as 
                    at the end of October 2003 to more than $12.09 million as at 
                    the end of October 2004. <br>
                    <br>
                    Notwithstanding a 17 per cent increase in aggregate 
                    provisions and reserved interest at RO386.86 million, 
                    provisional data reveals a 27 per cent increase in banking 
                    industry profit for the first 10 months of the current year. 
                    The provisional figures show a profit of RO74.7 million for 
                    the period ended October 31, 2004 as against RO58.9 million 
                    showed for the corresponding period of 2003. <br>
                    <br>
                    On a year-on-year basis, the aggregate bank credit grew by 
                    2.4 per cent to RO3.39 billion as against RO3.09 billion as 
                    at the end of October last year. Banks’ combined credit to 
                    public sector undertakings registered an increase of 21 per 
                    cent at RO82.9 million as against RO68.6 million. During the 
                    period under review, credit to the private sector witnessed 
                    a growth of 3.2 per cent at RO3.2 billion compared with 
                    RO3.07 billion. <br>
                    The asset side however shows a 23 per cent fall in cash on 
                    hand and deposits with the apex bank at RO115.34 million 
                    (RO149.83 million).</font></p>

