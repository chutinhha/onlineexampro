            engine of economic growth in general and of the tourism sector in 
            particular. Obviously, public investment in the tourism sector in 
            the current and certainly in the Seventh Five Year Development Plans 
            would be geared towards building this strategic partnership and 
            facilitate success by strengthening existing infrastructure, 
            promoting the incentive packages directed to the private sector, 
            both domestic and foreign, and augmenting the private sector 
            investment outlays in tourism projects.<br>
