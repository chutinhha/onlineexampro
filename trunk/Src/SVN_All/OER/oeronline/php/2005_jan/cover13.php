            Sultanate to more than 1 million at present. We expect this trend to 
            continue during 2005 and beyond. This is not wishful thinking – 
            rather, it is an optimistic outlook in view of the Sultanate’s 
            diversified tourism potential which include beaches, mountains, 
            habitat areas vast deserts, caves and waddis. These assets would 
            provide the Sultanate with great opportunities to develop a variety 
            of tourist products such as eco-tourism, adventure, golf tourism, 
            Saharan tourism, nature tourism and cultural tourism, to name only 
            some of the attractions. <br>
