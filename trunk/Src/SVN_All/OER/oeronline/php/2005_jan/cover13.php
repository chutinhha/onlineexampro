            private sector. Another key project in the pipeline is The Wave – 
            also a JV between the two sectors. Its components include, but are 
            not limited to, a golf course, three hotels, marina, commercial 
            centre, diving and water sport centre and residential areas. Work on 
            these two projects is already underway.<br>
