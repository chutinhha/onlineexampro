            start of a new long-term trend. In any case, we certainly will not 
            bottom out in terms of business improvement; we must continue to 
            improve every single day. The oil and gas business is exceedingly 
            complicated. It involves a lot of people and equipment, and it is 
            subject to the vagaries of nature and the world economy. Many 
            surprises lie in wait for us at every turn.</font></p>
