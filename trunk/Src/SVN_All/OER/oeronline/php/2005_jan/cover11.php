            <p align="justify"><font face="Verdana" size="1">From 100,000 
            barrels of oil per day in 1967, Petroleum Development Oman (PDO) 
            skilfully exploited several different developmental “waves”, based 
            on the exploration of new prospective areas and the exploitation of 
            cutting-edge technology, to reach more than 800,000 barrels of oil 
            per day in the late 1990s. The reservoirs in Oman are amongst the 
            world’s most geologically complex, encompassing the full range of 
            structures and oil types. So, exploitation of the country’s 
            hydrocarbon resources has always required a wide spectrum of 
            production techniques and state-of-the-art technology.<br>
&nbsp;<br>
            There has been considerable coverage in the media about the drop-off 
            in PDO’s oil production over the last few years. In all likelihood, 
            the year 2004 will be judged to have been a good one for PDO. We are 
            in a position to finish the year slightly above our average annual 
            production target of 650,000 barrels a day. And our capital 
            expenditure is likely to be within a few per cent of our budget. If 
            those two trends are borne out, then 2004 will be the second year in 
            a row in which we delivered what we promised in terms of oil 
            production and expenditure.<br>
            <br>
            <b>Production decline</b><br>
            Between 1998 and 2002, the annual production from PDO’s existing 
            oilfields with existing wells and hardware declined by an average of 
            15 per cent per year. That is, all other things remaining equal, we 
            could expect a field’s annual oil-production rate to be 85 per cent 
            of what it was at the end of the year before. Since 2003, however, 
            our efforts to improve the management of our wells and reservoirs 
            have had a noticeable effect on the decline rate. <br>
            <br>
            The production rate in 2004 is estimated to be 88 per cent of that 
            at the end of the year before, and we are striving to reach 90 per 
            cent in 2005. This improvement of 5 percentage points is the 
            combined result of not only well and reservoir management but also 
            what we call operational excellence. We’ve made similar strides in 
            producing what we call “new oil”, that is, oil from newly drilled 
            wells. The year 2004 will mark the first year since 2000 that we 
            produced more new oil than we did the year before.<br>
            <br>
            <b>Future production</b><br>
            Our oil-production forecast for the period between 2005 and 2009 is 
            very similar to the one showed last year. Conventional development 
            projects will contribute the bulk of oil production – more than 
            600,000 barrels per day. Some of these projects will be based on 
            waterfloods: a common method of maximising oil production by 
            injecting water into reservoirs to sweep the oil to producing wells. 
            This technique is enabling us to recover more than 55 per cent of 
            the oil originally in place in the Yibal field—a world-class 
            performance.<br>
            <br>
            By the end of the decade, we still aim to get back to around 800,000 
            barrels of oil per day, of which about 20 per cent or so will come 
            from enhanced-oil-recovery projects—primarily from the Harweel 
            miscible-gas injection project and secondarily from the Mukhaizna 
            steam-injection project. Another EOR project based on a different 
            steam-injection technique, at Qarn Alam, is expected to just come on 
            stream in 2009. These constitute the next “developmental wave” for 
            this company.<br>
            <br>
            Finally, we still are counting on our explorationists to discover 
            new oil, much as they discovered the Malaan field in north Oman this 
            year. By 2009, we expect exploration to have contributed about 
            20,000 barrels per day to our aggregate oil production. <br>
            <br>
            <b>Future savings</b><br>
            Last year we committed ourselves to a five-year plan that would 
            bring PDO back to producing 800,000 barrels of oil per day but at a 
            cost $2 billion less than originally budgeted, so that the average 
            cost per barrel of PDO oil would remain below $10. We took on the 
            challenge head-on by establishing a sweeping change programme known 
            as the Operating Model Review (OMR).<br>
            <br>
            To begin with, we made a commitment to take off 7.5 per 
            cent—equivalent to $150 million—from our original 2004 budget 
            without delivering a single drop of oil less than our production 
            target. I am happy to report that we have achieved that—and more. 
            The latest financial estimates put the total savings for the year at 
            $195 million—$36 million in operating expenditure and $159 million 
            in capital expenditure. I.e., every single day in 2004, on average, 
            we managed to save half a million dollars while delivering the 
            650,000 barrels oil and maintaining an uninterrupted supply of 
            natural gas. <br>
            <br>
            However, to stick with our business-improvement plans, we will have 
            to save twice as much from our original budget in 2005 than we did 
            in 2004: a total of $300 million dollars. We already have identified 
            ways of saving $177 million of those $300 million. So, with the $45 
            million in savings carried over from 2004, we have only to firm up 
            some $78 million dollars in savings next year.<br>
            <br>
            It is thus the period between 2006 and 2008 that will most require 
            our determination, inventiveness and cost competitiveness. During 
            that period, we will have to save a total of $1.3 billion from our 
            conventional operations. We are also expecting to save another $300 
            million dollars from our EOR projects—$40 million of which would 
            come in 2005.<br>
            <br>
            <b>The end of the beginning</b><br>
