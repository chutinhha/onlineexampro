<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Managing 
and leading in the 21st century</font></font><br>
<br>
<font face="Verdana" size="1"><b>Indications are that future organisations will 
be looking to recruit and develop individuals who are capable of fulfilling both 
roles—managing and leading—in different work settings</b><br>
<br>
<img border="0" src="im17.jpg" align="left" hspace="6" width="200" height="187">In 
most organisations, individuals are now expected to play dual roles: on the one 
hand, their goal is to focus on task and process; on the other hand, they are 
also encouraged to look for ways to enhance the future of the business by being 
creative ‘leaders’<br>
<br>
The 20th century revolutionised the concepts of leadership and management. As 
little as 10 years ago, leadership and management were clearly separated by 
status, role and hierarchy. Leadership was seen as something inherent, which 
could probably not be taught. It was the stuff of Bill Gates and Richard 
Branson, the giants of technology and commerce who belonged to a very exclusive 
club of like-minded individuals. These people manifested the kind of leadership 
that was connected to charisma, power and the ability to inspire others to buy 
into their vision. <br>
<br>
In the past, you needed the right connections or at least an MBA to become a 
leader of anywhere near that calibre. The ethos was driven by political 
visionaries such as Margaret Thatcher and Ronald Reagan who promoted the ideas 
of free enterprise and a move away from socialist economies.<br>
<br>
Privatisation swept across the world. In Britain, organisations such as the 
British Airports Authority were put on notice by the then Thatcher government 
that public money would be withdrawn. A major culture change ensued. The entire 
public sector organisation was transformed into a multi-million dollar 
enterprise. The major British airports such as Heathrow, Gatwick, Glasgow and 
Edinburgh graduated from semi-military establishments focusing on the running of 
efficient, safe airports to retail malls where passengers could avail themselves 
of a huge variety of goods and services. <br>
<br>
The changes were led and introduced by Sir John Egan, a captain of British 
industry. He was selected because of his track record with Jaguar cars and the 
way he transformed that organisation from its ailing state in the 1970s to its 
major accomplishments of the late 1980s and early 1990s. The same pattern was 
repeated in the US and Europe with organisations such as Nokia, SAS, Xerox, IBM, 
Novartis, ICI and Unisys – to name but a few. <br>
<br>
Soon after privatisation in 1984, the British Airports Authority changed its 
name and its attitude to leadership and management. They became BAA. One of the 
first initiatives of the executive team at Wilton Road—BAA’s headquarters—was 
the introduction of a company-wide programme called ‘sharing the vision’. Their 
business was growing and they could not depend on the executive team of an 
ex-automobile entrepreneur to take on the responsibility of managing six 
expanding airports. <br>
<br>
It became clear that those who knew more about the airport business were those 
who worked at the ‘coalface’. BAA turned to the concepts of Japanese management, 
Kaizen (continuous improvement), to launch further initiatives that would 
transform line managers into leaders as well as managers.<br>
<br>
Since the 1990s, organisations like BAA have moved on. They have become more 
complex. Instead of reporting to just one boss, individuals may now be part of a 
matrix organisation where they report to several bosses with dotted-line 
relationships to other managers.<br>
<br>
Well-known gurus such as Charles Handy, Rosabeth Moss-Kanter and Tom Peters 
began to write about these organisations that had to respond to the changing 
world around them. The changes were driven by new economic realities and new 
technology. A prime focus for change was the need to compete for business and 
changing customer requirements. The post-industrial monolithic organisations 
were doomed to extinction. <br>
<br>
Technological development and IT had added new dimensions to the 
workplace—speed, efficiency, remote working and virtual teams—so that customers 
could be offered what they really wanted, i.e., quality products and outstanding 
service at a very fast pace across the globe. The idea of employees blindly 
following their bosses began to disappear. Leadership and management evolved 
into something more diffuse, shared, rotated and delegated in flatter 
organisational structures in line with e-commerce.<br>
<br>
Today, organisations are tasked with developing both leadership and management 
skills in individuals who are accountable for taking their departments, teams or 
projects forward. The degree to which they operate in either mode is very much 
dictated by the circumstances of each organisation. <br>
<b><br>
The manager/leader differences </b><br>
Leadership is very much about articulating the ‘big picture’. It involves the 
idea of anticipating the future and promulgating the vision or mission of the 
business often driven by changing customer needs. Management, on the other hand, 
is more about interpreting the big picture through identifying ‘tactics’. While 
leadership answers the ‘why?’ question—e.g., ‘why are we in 
business?’—management addresses the ‘how?’ question—e.g., ‘how do we implement 
the vision in practical terms?’<br>
<br>
Leadership seeks to empower people to make their own choices, even to encourage 
them to contribute ideas to the vision and mission. Management is more a case of 
delegating work downwards. Leaders create, and managers ‘fix things’. Leaders 
are often involved in innovation while managers are more concerned with the task 
or process. Leaders anticipate and initiate change and managers implement the 
change and reengineer processes. Leaders transform the department or 
organisation as managers maintain and sustain levels of productivity.<br>
<br>
Leaders coordinate while managers control work and work processes. The leader’s 
job is to develop the business and the business strategy – e.g., formally or 
informally researching and identifying new business opportunities. Managers are 
more involved in setting objectives, drafting plans and implementing the work. 
Inspiring people is very much part of a leader’s role while, at the same time, 
managers must know how to motivate staff on a daily basis. <br>
<br>
Leaders coach and develop – e.g., by setting up mentorship relationships between 
new entrant graduates and experienced, successful senior managers. Managers on 
the other hand are engaged in training and managing/ monitoring staff 
performance. The leader’s role is to renew, reinvent and replenish while 
managers measure, evaluate and monitor. Twenty-first century leadership is also 
about growing and developing self-managing teams. Managers are more concerned 
with building and managing conventional teams.<br>
Very often during management workshops, individuals take a self-assessment test 
to measure their leader/manager profile. The majority discover that they are 
operating as both leaders and managers. <br>
<br>
One exciting characteristic of 21st century organisations is diversity. No two 
organisations are the same. There is no longer a clear-cut set of boundaries 
demarcating the roles and functions of leaders and managers. In most 
organisations, individuals are now expected to play dual roles: on the one hand, 
their goal is to focus on task and process; on the other hand, they are also 
encouraged to ‘think outside of the box’ – look for ways to enhance the future 
of the business by being creative ‘leaders’. This is not just a fashion or 
trend. It reflects the growing awareness that managers and staff often know more 
about the work, business and what customers need than those who are higher up in 
the hierarchies.<br>
<br>
Twenty-first century organisations have thus evolved into flexible networks of 
enterprise and productivity. Enterprise represents leadership while productivity 
represents management. All the indications are that the trend will continue and 
that organisations will be looking to recruit and develop individuals who are 
capable of fulfilling both roles in different work settings.</font></p>

