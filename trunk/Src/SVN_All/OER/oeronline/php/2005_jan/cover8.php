            <p align="justify"><font face="Verdana" size="1">If 2004 is any 
            indication of what the future holds for Oman, then 2005 looks like 
            it will be an exciting year indeed. There have been numerous recent 
            changes to Oman’s legislative landscape, with still other changes on 
            the horizon. <br>
            <br>
            <b>Private TV &amp; radio firms</b><br>
            As its name suggests, Sultani Decree 95/2004 promulgating the Law 
            Governing Private TV and Radio Stations in Oman allows the private 
            ownership of TV and radio stations by Omanis. Specifically, the new 
            law provides that a private radio or TV station may only be 
            established in Oman or its territorial waters after first obtaining 
            a licence to operate. The only corporate form available to stations 
            desiring to obtain such a licence is that of a closed joint stock 
            company (SAOC), wholly owned by Omani nationals. <br>
            <br>
            Although Sultani Decree 04/74 promulgating the Commercial Companies 
            Law (CCL) requires an SAOC to have at least three natural or 
            juristic shareholders, and a minimum capital of RO500,000, stations 
            licensed under the Law Governing Private TV and Radio Stations may 
            not transfer or otherwise dispose of its shares to a non-Omani 
            party. Furthermore, the licence application submitted by the station 
            must contain the particulars of the station, including the type of 
            radio or TV service provided, the place of broadcasting, the areas 
            covered by the broadcast, the means of broadcasting, the target 
            audience of the broadcasts, and the broadcasting hours and timings.<br>
            <br>
            The licence application must also be accompanied by an undertaking 
            by the station that it will implement the directives of the 
            Committee formed by the Law Governing Private TV and Radio Stations 
            in order to fulfil the needs of the community. Despite these 
            limitations the new law should pave the way for exciting new changes 
            in radio and TV broadcasts within Oman, broadening the variety of 
            programs available to the population.<br>
            <br>
            <b>The sector law</b><br>
            Although Oman already had a privatisation law in effect, the 
            promulgation of the new Sector Law has clarified several issues 
            which the previous law did not. Specifically, the new Sector Law 
            addresses in detail the manner in which employees of a privatised 
            entity are to be dealt. According to the law, no redundancy with 
            respect to such employees shall be allowed for a period of five 
            years following the privatisation or restructuring of the public 
            entity. This is significant considering that the main concern 
            relating to privatisation is that private sector entities only care 
            about making money. This law in effect provides an important 
            protection for the employees of the formerly public entity.<br>
            <b><br>
            E-legislation &amp; e-government</b><br>
            Oman’s push to promulgate legislation governing e-commerce and other 
            IT-related laws seems to be moving forward, and many within the 
            business community are already preparing for big changes in the way 
            business is conducted in Oman. The acknowledgement of the validity 
            of digital signatures, and the recognition of the authenticity of 
            electronic documents will be important steps into the future for 
            Oman. If the provisions of the UNCITRAL Model Laws are incorporated 
            into such legislation, it should help pave the way for Oman’s global 
            electronic integration. <br>
            <br>
            Likewise the push to digitise national ID cards and to create an 
            e-government portal is progressing as well. Such efforts will help 
            to streamline numerous tasks such as company registration, possibly 
            allowing a company to be formed almost entirely online. Likewise, 
            governmental forms may be easily accessible and downloadable, 
            negating the need to physically travel to a ministry to obtain the 
            required forms. The obvious increases that such a system would offer 
            are numerous, and would be welcomed not just in the legal community 
            but throughout Oman.<br>
            <br>
            <b>Property ownership issues</b><br>
            This topic, which has been the subject of numerous articles and 
            discussions in Oman, continues to be of paramount interest to many, 
            especially within the expatriate community. Although the first step 
            to property ownership reform has been taken (namely the ability of 
            GCC nationals to own land in Oman), the promulgation of a new law 
            allowing non-GCC nationals to own land in Oman has yet to happen.<br>
            <br>
            If a new law is enacted to allow non-GCC nationals to own land 
            within Oman, the extent of such ownership remains unclear. Although 
            unrestricted ownership of land is a possibility, it is more likely 
            that the right to own land will be restricted to certain specified 
            developments (such as the Muscat Golf and Country Club). Whether 
            land ownership by expatriates will materialise remains to be seen, 
            but this is definitely a space worth watching.<br>
            <br>
            <b>The future generally: Tourism</b><br>
            The establishment of the Ministry of Tourism is also an important 
            step in Oman’s economic development. Oman’s efforts to diversify its 
            economy should be aided considerably by the establishment of the 
            ministry. This, combined with the potential property rights reform, 
            should help to promote tourism immeasurably. From a legislative 
            standpoint, there have been several significant changes in Oman. If 
            Oman can continue its course, we should see some even more 
            significant changes in the near future.</font></p>
