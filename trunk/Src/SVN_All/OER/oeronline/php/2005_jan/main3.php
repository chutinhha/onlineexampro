<p align="justify"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4" color="#FF0000">A drive into the past</font><font face="Verdana" size="1"><br>
<br>
<b>Praveen Thampi provides a rear-side view of the history of the automobile and 
explains just why cars are such a passion with us</b><br>
<br>
<img border="0" src="im19.jpg" align="left" width="125" height="147">The 
opportunity to talk cars was pretty much exciting for me, and OER, despite my 
sounding them of my amateur writing skills, quite fit me into reflecting some of 
my excitement in this article. Armed with the brief of writing a monthly column, 
I settled down on my plan to begin at the beginning, and did my quick reference 
checks.<br>
<br>
Cars have always been objects of interest for us mortals. I meet a lot of people 
from different walks of life during my normal day, and I am yet to come across 
anyone who actually does not care about cars. That certainly is no shocking 
statement considering the varied shapes, sizes and colours they come in and the 
unimaginable functionality and safety they provide. What started off as a simple 
replacement to the horse-drawn carriage, a mere solution to move people and 
things from point A to B, has now turned out to be much more than just a 
mobility solution.<br>
<br>
Today, car is so much a part of a person that it is a true extension of 
personality and lifestyle – it reflects the attitude towards life, and it also 
does things beyond what we ever imagined or dreamed. Interestingly, when it 
comes to cars, today we can afford to dream and also make our dreams a reality. 
My mom told me long time ago that I can’t have the cake and eat it too, but it 
sounds like a possibility here. We reserve for the car an emotional relationship 
we do not share with other machines – not with refrigerators, airplanes or even 
the curiously human ‘personal’ computer. The car has been venerated as an object 
of affection, excitement, even love. And when it fails us, we heap upon it the 
bitterness and intense anger we generally reserve for our loved ones.<br>
<br>
If you are middle-aged, chances are that your grandparents were born before the 
automobile worked its enormous change on our lives. In a span of three 
generations, electricity, telephone, radio, TV, airplanes, computers, even space 
travel, have changed the way we live beyond recognition. But no invention has 
changed our daily life quite so pervasively as the automobile. <br>
<br>
<b>Dreams on wheels</b><br>
The history of the automobile is very rich, and dates back to the 15th century, 
when Leonardo da Vinci was creating designs and models for transport vehicles. 
The basic premise of the automobile was simple; choose a wheeled vehicle from 
the many types typically pulled by horses or oxen, add a motor and create a 
self-propelled, personal transportation vehicle. There are many different types 
of automobiles—steam, electric, and gasoline—as well as countless styles. <br>
<br>
As early as 1769, Nicolas Cugnot fitted a wagon with a steam engine, fired it up 
and ran it across a field in France, thereby becoming the first person to make 
and operate a powered vehicle. Incidentally, Cugnot’s machine did not handle 
well and he ran it into a wall – the world’s first auto accident. Legend has it 
that he was arrested for his trouble and so might also be credited with the 
world’s first moving violation.<br>
<br>
Within available information, the earliest ancestor of the modern automobile is 
probably the Fardier, a three-wheeled, steam-powered, 2.3-mph vehicle that was 
built in 1771 by Nicolas Joseph Cugnot, for the French minister of war. This 
machine was cumbersome and was never really put into production because it was 
much slower and harder to operate than a horse-drawn vehicle. Amedee Bollee, 
also a Frenchman, built an improved version to the Fardier, a 12-passenger steam 
car in 1873, but this steam engine proved impractical for a machine that was 
intended to challenge the speed of a horse-and-buggy. <br>
Nicolaus August Otto invented the gas motor engine in 1876. In 1885, Gottlieb 
Daimler invented a gas engine that allowed for a revolution in car design. Who 
invented the first automobile is a tricky question that does not have a 
straightforward answer. In my opinion, Karl Benz, a brilliant German mechanical 
engineer who designed and built the world’s first practical automobile to be 
powered by an internal-combustion engine in 1885, deserves this title the most.
<br>
<br>
<b>The modern automobile</b><br>
Benz was the first to put an automobile into production, a three-wheeler he 
built in 1885. Just when his first delivery took place is not clear. It seems 
likely that it took place in spring of 1886. In fact, when Benz first got his 
three-wheeler chugging into movement in Mannheim, it was not the first time a 
vehicle had propelled itself. What was new was that Benz perfected his machine; 
put it into production and people bought it.<br>
<br>
Gottlieb Daimler and Wilhelm Maybach built the world’s first ‘practical’ 
automobile with a workable internal combustion engine in Germany in 1889. 
Powered by a 1.5-HP, two-cylinder gasoline engine, it had a four-speed 
transmission and the best speed it could travel was at 10 mph. Karl Benz also 
built a gasoline-powered car the same year. This kind of gasoline-powered 
automobile or, in other words, a motorcar remained largely a curiosity for the 
rest of the 19th Century, with only a handful being manufactured in Europe and 
the US. <br>
<br>
America’s first gasoline-powered automobile was the 1891 Lambert car invented by 
John W. Lambert. He founded America’s first company to manufacture and sell 
gasoline-powered vehicles. The first automobile to be produced in quantity was 
the 1901 Curved Dash Oldsmobile, which was built in the US by Ransom E. Olds. 
Modern automobile mass production and its use of the modern industrial assembly 
line, is credited to Henry Ford of Detroit, Michigan, who built his first 
gasoline-powered car in 1896. <br>
<br>
Ford began producing his Model T in 1908, and by the time it was discontinued in 
1927, over 18 million had rolled off the assembly line. Rudolf Diesel invented 
the diesel-fuelled internal combustion engine. Charles F. Kettering invented the 
first automobile electrical ignition system and the first practical 
engine-driven generator.<br>
<br>
A lot has happened since Benz got that first car working well enough to put it 
into production. But it hasn’t really been that long a time. Think for a moment 
about the world of 100 years ago. Travel was slow and difficult. A trip to 
anywhere was a major undertaking. Most roads were just gravel or dirt. Cities, 
usually built along streams or lakes or rail lines, were compact and congested.<br>
<br>
They were quickly transformed by the car into sprawling urban complexes, held 
together, and in some ways split apart, by the freeways and interstates that in 
fact bind them. Buildings have been razed and farmland paved over to provide 
parking for the millions of vehicles we own. Along those roads are motels (motor 
hotels), fast-food restaurants, shopping malls, drive-in movies, drive-in banks, 
and drive-in florists – even drive-in funeral homes. In this century, the auto 
has spawned whole industries where none were before.<br>
<br>
<b>The (r)evolution</b><br>
There is so much more to go when it comes to automobiles. With needs changing 
for both manufacturers and customers, evolution was the only way forward. The 
designers of this world made sure that cars took shape in line with the wildest 
imaginations people had. They designed cars for the young and old, serious and 
fun loving, the father, mother, son and daughter. They even designed cars for 
the handicapped and the working folk. They strive hard to satisfy the poor as 
well as the superrich, and also joined hands for entertainment with various 
motor sports. <br>
<br>
Companies evolved and merged to create value for the end-user. What we wanted 20 
years ago is not a ‘want’ anymore, but a basic criterion taken for granted. 
Situations changed, and what we aspired in a car a few years ago has become a 
standard today. For manufacturers, it was a dire need to be seen in this highly 
competitive world of cars and to be the survivor, as we all like it to be.
