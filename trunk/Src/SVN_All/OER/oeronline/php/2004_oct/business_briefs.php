                    Free Trade Zone in the pipeline </b><br>
                    The Arab Gulf Cooperation Council (AGCC) countries and India 
                    are exploring the possibilities of creating a free-trade 
                    area between the two. The Gulf is one of the largest markets 
                    for imports worldwide, and will translate into a huge 
                    potential market for India. India accounts for nearly 5 per 
                    cent of Oman's total imports, which rose by 8 per cent in 
                    2003 to over RO2.6 billion. The country's exports registered 
                    a growth of 4.4 per cent at RO4.48 billion, showing a trade 
                    balance of RO1.87 billion and imports from India stood at 
                    over RO110 million last year. Oman's major import partners 
                    are the UAE and Japan. The Sultanate imported goods worth 
                    RO545 million from the UAE in 2003 and goods worth RO432 
                    million from Japan, constituting 38 per cent of Oman's total 
                    imports. The proposal of free trade agreement has already 
                    been agreed in principle and a framework, providing overall 
                    commitment for broad-based economic cooperation between 
                    India and member countries of the Gulf was signed in New 
                    Delhi in late August. </font></p>
