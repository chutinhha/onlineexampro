            Education integrates the ICDL programs </b><br>
            <img border="0" src="im11.jpg" align="left" hspace="5" width="125" height="121">The Ministry of Education in the Sultanate of Oman has unveiled its 
            endorsement of the International Computer Driving Licence (ICDL) 
            program and its adoption in all its public schools effective this 
            academic year, as a standard for upgrading the computer skills in 
            the education sector across the Sultanate, following the success of 
            the program in other parts of the Gulf region. ICDL has also made 
            rapid strides in raising IT literacy all over the world. This 
            follows the signing of an agreement between the Ministry of 
            Education and ICDL Oman. The agreement was signed by HE Yahya bin 
            Saud El Sulaimi, Minister of Education, Sultanate of Oman, and Jamil 
            Ezzo, Director General of ICDL GCC Foundation, the regional 
            governing body and the operator of the ICDL program in the Gulf 
            States. ICDL GCC Foundation recently opened its ICDL Oman office in 
            Knowledge Oasis Muscat, as part of its overall strategy to establish 
            local offices in each GCC member state for promoting IT literacy and 
            providing local support to the ICDL program on the ground.<br>
            <br>
            According to the recently signed agreement, the Ministry of 
            Education will work in partnership with ICDL Oman to integrate the 
            ICDL standards and syllabus in its teaching of computer to 11th 
            grade students. As first phase to this ambitious project that calls 
            to making ICDL obtainable by all teachers and students, ICDL Oman 
            will begin by initially accrediting 360 schools as ICDL approved 
            training centres and 12 schools as ICDL approved testing centres. 
            With this project aiming to train over 50,000 students annually, the 
            Ministry is providing each 11th grade student with ICDL approved 
            training material that includes courseware books and access to ICDL 
            interactive courses. These courses are hosted on a Learning 
            Management System (LMS) that will track the student's learning 
            progress. The Ministry will also urge teachers to obtain the ICDL 
            certification regardless of their subject matter. <br>
            <br>
            HE Mustafa Bin Ali Bin Abdullatif, Undersecretary for Administration 
            and Finance, Ministry of Education, commented: &quot;Providing students 
            with the right tools to enhance their computer skills is a major 
            priority for us and we seek to achieve this goal by adopting proven 
            models, such as the ICDL program, which has attained global 
            recognition as one of the most effective syllabus in this field. We 
            are confident that our efforts in furthering IT literacy will 
            accelerate our pace to reaching an eSociety.&quot; <br>
            <br>
            The initiative is greatly supported by the Ministry of National 
            Economy, which is the apex body for enhancing IT competency in the 
            government as well as the overall society in the Sultanate. The 
            Ministry of Education and the Ministry of National economy will work 
            closely with ICDL Oman to put in place the necessary technological 
            infrastructure and ensure that all the training and testing centres 
            adhere to the international standards. In addition, all government 
            organisations in Oman are exerting tremendous efforts to providing 
            e-government service to its citizens. <br>
            <br>
            The ICDL certification comes as part of the strategic plan followed 
            by Ministry of National Economy to furthering computer literacy and 
            providing all parts of society to benefit from the today's 
            technologies and information access. </font></p>
