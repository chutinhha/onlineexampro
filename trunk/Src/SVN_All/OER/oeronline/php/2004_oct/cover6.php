            Smaller banks need to be aggressive<br>
            </font><font face="Verdana" size="1"><br>
            </font><b><font face="Verdana" size="2">Hassan Mohsin <br>
            Senior Vice-President, Asset Management Division, Fincorp </font>
            </b></p>
            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im43.jpg" align="left" hspace="5" width="150" height="183">What impact will 
            the merger have on the competitiveness of banks in the country?<br>
            </b>I believe that the other banks will have to be more aggressive 
            after this merger as BankMuscat is in a very strong position to 
            dictate terms. The other players in the banking field in Oman will 
            have to increase their services or be left behind. As far as 
            financing of projects and lending was concerned, BankMuscat was the 
            leader even before the merger. But seriously, I feel that the 
            competition will in fact lessen after the merger. The banking 
            industry in Oman is now in a state of semi-monopoly. <br>
            <br>
            With the merger between the two leading banks in the Sultanate, 
            BankMuscat will obviously have a big edge in terms of getting 
            business. The other banks can just react. So, I see a 
            ‘follow-the-leader’ kind of situation emerging in the banking sector 
            as far as Oman is concerned. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What, according 
            to you, are the reasons behind this merger? </b><br>
            I believe that one of the major reasons behind the merger of these 
            two large financial institutions is regional aspiration. There is 
            now a very good case for BankMuscat to go regional as there is only 
            that much business to be done in the Sultanate. One of the other 
            reasons can be the issue of overheads. Both BankMuscat and NBO were 
            incurring expenditure in trying to compete with each other in the 
            market. Now, that cost will be completely wiped off. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>How will the 
            rest of the local banks react to this merger? </b><br>
            I do not think that there will be any immediate or drastic step 
            taken by the other banks in reaction to the merger. Let us not 
            forget that there is a market for the smaller players and their 
            management seems happy with the business they are able to generate. 
            The other banks have not been aggressive in the past because the 
            economy was not growing too much and within these limitations, they 
            were doing steady business. But in their own interest, these banks 
            need to innovate, offer more services and be aggressive in their 
            marketing. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Do Omani banks 
            have a chance to compete at the regional level once a common GCC 
            market comes into force? <br>
            </b>Omani banks stand a good chance of competing at the regional 
            level. The banks are able to meet the minimum global standards. The 
            merger between BankMuscat and NBO will add muscle to the banking 
            scenario in the Sultanate and send the right signals to investors in 
            the region. Having said that, size alone is not the only parameter. 
            A high degree of sophistication is what BankMuscat should aim at. 
            This sophistication has to be at several levels – structuring of 
            finances, customer relationships, providing awareness and beefing up 
            services. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What would be 
            the next logical step for BankMuscat?</b> <br>
            The first logical step for BankMuscat would be to establish its 
            position as the leader and work on having a clear-cut focus in its 
            business objective. The bank will also concentrate on its branches 
            in Abu Dhabi and Cairo. I don’t see any great chances of 
            acquisitions or mergers happening in the near future. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Do you think 
            there will be any perceptible impact on the end-consumer?</b> <br>
            No, I do not think so. The merger holds more significance for the 
            government and the corporate sector rather than the end-consumer.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Will the merger 
            have any significant impact on the country’s economy?</b> <br>
            Yes. If the merger leads to formation of business relationships and 
            a client base overseas, then the economy will get a boost. With a 
            regional presence for an Omani bank, there will be a support system 
            for the otherwise oil-based economy. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Are there any 
            niches that Omani banks may look at occupying after a common GCC 
            market becomes a reality? </b><br>
            I believe that banking in Oman has been lacking in investments and 
            lending. After a common GCC market becoming a reality, banks in Oman 
            will have to increase their ability to fund projects, increase 
            lending and also play an advisory role. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What is the 
            biggest challenge for the Omani banking sector today?</b> <br>
            Banks in Oman rely too much on consumer lending, which is not going 
            to increase much. The banks have to change their focus from the 
            small retailer and small avenues for investment. Omanis are not 
            great savers; so, the consumer base alone cannot lead to growth.</font></p>
