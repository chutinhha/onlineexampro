<img border="0" src="im27.jpg" align="left" hspace="5" width="150" height="114">What does it mean to &quot;build 
strong partnerships&quot;? Why do you need to master this vital skill? What practical 
steps can you take to achieve it, right now? <br>
<br>
Ron Kaufman, Asia's leading management &quot;guru&quot; for service and partnership 
innovation will answer all these pertinent questions and more when he visits 
Muscat in December.<br>
<br>
The Oman Economic Review is the the media partner for the event. Gazelle 
International in association with Right Selection brings has invited Kaufman to 
hold series of public seminars on topical issues such as Superior Customer 
Service, Sales and Marketing, Leadership and Strategy, Teamwork and Motivation.
<br>
<br>
A stand-up comic, a guru and an entertainer all rolled into one, Kaufman will be 
unleashing one his most popular and practical seminars: Up Your Service! This is 
a full day seminar, based on his best selling book of the same title. <br>
<br>
In a welcome departure from conventional management seminars which often put the 
participants to sleep due to excess theory, Kaufman's inimitable humorous style 
coupled with practical techniques and real life examples will be a refreshing 
change.<br>
<br>
Up Your Service! demystifies what it takes to achieve spectacular service. It's 
not about smiling wide and working hard. You must understand the critical 
elements that make a service culture succeed. Increasing customer loyalty, 
building strong partnerships and creating dynamic cultures should be the goals 
of every forward thinking organisation. <br>
<br>
In Kaufman's own words: &quot;I bring these elements to life with an entertaining 
approach that keeps the participants learning. Up Your Service! is also a hard 
hitting knock-on-the-head for the bureaucratic, go-slow mindset. Such people are 
a disaster in our fast-paced and customer-focused service economy. The sooner 
they go extinct, the better for everyone!&quot; <br>
<br>
Up Your Service! is a delightful yet value-packed handbook of the Dos and Don'ts 
of customer relationship management. The ultimate objective of CRM (Customer 
Relationship Management) if you don't already know is to build greater share of 
customer, and thereby bigger profits. Its focus is profitability, rather than 
revenue.<br>
<br>
Kaufman introduces a unique concept called the, &quot;Service Encounters of the Third 
Kind.&quot; This in a nutshell is the exploration of the customers' future needs and 
interests. But first, let's look at what Kaufman means by &quot;Encounters of the 
First&quot; and the &quot;Second Kind.&quot; <br>
<br>
<b>Service Encounter of the First Kind: </b><br>
Kaufman believes that a company should approach the customer with the most basic 
of all customer service questions: &quot;What do you want?&quot; <br>
<br>
The customer is bound to reply with equal simplicity, &quot;I want your product &quot;X&quot; 
by time and date &quot;Y&quot; at your listed price &quot;Z&quot;. <br>
<br>
The company's priority and service focus should then be clear: &quot;Get the 
customer's order right, and get it right the first time!&quot; <br>
<br>
Campaigns to accomplish this objective are widespread and easy to spot. &quot;100 per 
cent Right!&quot;, &quot;Work Perfect&quot;, &quot;Zero Defects&quot; and &quot;Six Sigma Quality&quot; are all 
examples of slogans companies use to focus their workers on getting the basics 
right, first time, each and every time. <br>
<br>
But in this kind of a relationship, breakdowns in the service delivery system 
are bad news. They are to be ferreted out, analysed, problem-solved and, most of 
all, eliminated. The service system must be streamlined and standardised in 
every possible way. <br>
<br>
Companies that consistently succeed in this undertaking (delivering your product 
&quot;X&quot; by time and date &quot;Y&quot; at your listed price &quot;Z&quot;) earn their reputations in the 
market as steady and reliable suppliers. This leads, as it should, to customer 
satisfaction. <br>
<br>
But this kind of customer encounter has an inherent fault. There are bound to be 
some customers with questions and unusual requests and this can lead to the 
slowing down of the whole system. No wonder so many customer requests for 
anything &quot;out of the ordinary&quot; are met with the retort: &quot;We don't do it that 
way.&quot; This obviously makes the business suffer. <br>
<br>
<b>Service Encounter Of The Second Kind: </b><br>
In &quot;Service Encounters of the Second Kind&quot;, the company approaches the customer 
with a question that goes beyond standard offers of &quot;X&quot; product at &quot;Y&quot; time and 
&quot;Z&quot; price. Instead of only the basic question &quot;What do you want?&quot; the service 
representative poses an additional, and more inviting question: &quot;How do you want 
it?&quot; <br>
<br>
The company's priority and service-focus now changes. According to Kaufman, the 
company will now deliver what the customer requests, just the way the customer 
requests it! Special products, unique combinations, odd-hour deliveries, 
different schedules for pricing and/or payment; all are challenges for the 
service team to understand, and accomplish. <br>
<br>
With these &quot;Service Encounters of the Second Kind&quot;, breakdowns in the service 
delivery system are to be expected at first. The service system improves, not 
through vigorous efforts to standardise, but through willingness and commitment 
to customise! <br>
<br>
<b>Service Encounter Of The Third Kind: </b><br>
In &quot;Service Encounters of the Third Kind,&quot; the company welcomes the customer in 
a manner completely different from the standardised &quot;What do you want?&quot; or 
customised &quot;How do you want it?&quot; The company looks to the customer with 
sincerity, interest and patience, and asks the somewhat unlikely question: &quot;What 
do you want to become?&quot; This question is bound to catch most customers on the 
wrong foot as many do not know the answer. &quot;Actually we are not entirely sure 
yet,&quot; is the likely response. And then, availing themselves of the sincerity and 
interest the company shows they might add &quot;Could we talk about it together?&quot; <br>
<br>
The question, and their response, opens the door to a new and collaborative 
conversation; a Service Encounter Of The Third Kind. <br>
<br>
In Up Your Service! Kaufman expands his service perspective to the Internet, 
insisting that, &quot;the World Wide Web makes providing service easier than ever 
before,&quot; but cautions, &quot;customers can leave your site and visit your 
competitors' easily, too. <br>
<br>
