            <font size="4" color="#FF0000" face="Verdana">Partner or perish?</font><p align="justify"><font face="Verdana" size="1">
            <b>MCE's John L. Mariotti reasons why partnerships are one of the 
            most powerful means to build stronger ties between organisations in 
            a world of diverse cultures and business practices </b><br>
            <br>
            <img border="0" src="im31.jpg" align="left" hspace="5" width="125" height="173">These are difficult times. Change is more rapid than ever, and 
            economic uncertainty is a constant condition. The globalisation of 
            business took a blow to the solar plexus with terrorist attacks, but 
            when business temporarily goes down for the count, it always gets 
            up. <br>
            <br>
            Recessions and downturns inevitably run their course and are 
            followed by recoveries and prosperity for those who prepare well. 
            When business gets up off the mat, one fact emerges clearer than 
            ever, more prominent than ever - people and organisations in free 
            enterprise economies are resilient. <br>
            <br>
            <b>Are partnerships necessary? </b><br>
            Are we, however, beginning to see the end of an era of trust, and 
            with it the imminent decline of partnerships? For years opponents of 
            partnerships have called them foolish clichés. We all know of 
            untrustworthy charlatans, pretenders, and executives who would 
            double cross a partner to get ahead. In the long run, these people 
            get what they deserve, but in the short run, they wreak havoc. We 
            saw the recent explosion of e-commerce lead to a glut of hastily 
            formed partnerships; then saw many of these partnerships dissolve, 
            victims of high-tech, but at best, low-profit ventures. Such 
            examples lead to the pivotal question: are partnerships a boon or 
            bane?<br>
            <br>
            It is my contention, nevertheless, that we need partnerships. In a 
            world of widely diverse cultures and business practices, 
            partnerships are one of the most powerful means to build stronger 
            ties between organisations. This is borne out by the fact that 84 
            percent of the respondents to a recent Forrester Research study, 
            cited in Business 2.0, identified that the aspect of their business 
            most likely to grow in the next year would be partnerships.<br>
            <br>
            <b>Types of partnerships </b><br>
            Every organisation needs partners - the complexity and speed of 
            modern commerce means that no organisation alone can be good enough 
            at everything it needs to do. There are four fundamental types of 
            partners - Suppliers, Customers, Employees, and Alliances of both a 
            corporate and personal nature. In business, you can't make something 
            from nothing. That means every enterprise needs suppliers to provide 
            the materials and/or the services it requires to do whatever it 
            does. And without customers, there is no purpose to any of this, so 
            customers are equally essential. Of course all organisations need 
            people or employees working together to create and deliver the value 
            that customers will pay for. Last, but far from least, many 
            businesses, in order to grow, need to form business-to-business 
            alliances, and have special partners with whom to cooperate in a 
            desire to realise shared goals. People must also form 
            person-to-person alliances with trusted confidants who can provide 
            truthful feedback when it matters.<br>
            <br>
            <b>Partnership drivers </b><br>
            For a partnership to form there must be drivers which are based on 
            the needs of the prospective partners. These drivers come in many 
            forms but the five most common types are: routes to markets, access 
            to technology, physical resources such as capacity, human resources, 
            and financial resources. <br>
            <br>
            A common situation that drives a company to seek a partner occurs 
            when that company simply cannot, for whatever reason, gain access to 
            certain customers or markets. If the right kind of partnership is 
            formed, one partner brings entry into the market, and the other 
            brings a product or service that fulfils a market need and 
            complements its partner's offerings. The amazon.com and Toys &quot;R&quot; Us 
            partnership was one of these. Toys &quot;R&quot; Us was struggling with its 
            e-business programme and amazon's toy ventures were failing. The 
            partnership provided both partners with a better solution.<br>
            <br>
            In other cases, one partner can benefit from the technological 
            expertise of the other in order to bring something novel to market. 
            Proprietary technology from one partner is joined with strong 
            production, marketing or commercialisation skills of another, and 
            this often leads to a winning basis for a partnership. 
            Pharmaceutical companies frequently partner with providers of highly 
            specialised delivery systems for drug delivery. GlaxoSmithKline uses 
            Alza's (a Johnson &amp; Johnson company) transdermal patch technology in 
            its Nicoderm CQ - a popular 'stop-smoking' product. <br>
            <br>
            The drivers of physical resources, human resources, and financial 
            resources are almost universally used in partnership arrangements, 
            but to varying degrees. Companies may share storage facilities, or 
            productive capacity, preferably in non-competing markets. Temporary 
            employees make up the shared work forces in many organisations. This 
            levels seasonal variations and helps overcome staffing shortages. Of 
            course, financial resource sharing is common to most entities; 
            whether this is truly a 'partnership' might be disputed. The fact 
            that it is necessary is indisputable. I would argue that, in ideal 
            cases, lenders and financiers are indeed partners with borrowers.
            <br>
            <br>
            <b>Strategy and selection </b><br>
            Most businesses start because someone has an idea to create 
            something of value that others want and will pay for. That concept 
            is the starting point upon which strategy is formed, and from which 
            partnerships become inevitable. Successful businesses grow beyond 
            the resources and capability of the founder fairly quickly. Those 
            entities that succeed over the long term are put in the hands of 
            successors who are both leaders and partners. The newly founded 
            business won't work very well, for very long, in any other way - if 
            it grows in a healthy fashion, it quickly outstrips the reach and 
            capabilities of the founder. <br>
<br>
Selecting the right partners is one of 
            the most critical decisions any business and its managers/owners can 
            make. Once chosen, building trust with the right partners is 
            essential. Everything else will follow - open communications, 
            fairness and honesty, balanced risks and rewards - but if the choice 
            of partner is wrong, and trust is not built, the business will not 
            grow as it should. <br>
<br>
What is the businessperson to do in the face of 
            all these issues? You can't simply start again - or can you? You can 
            assess who your partners are, and the quality of the partnerships 
            you have built with them. If you find partners you would not have 
            chosen, then you must decide whether to improve those partnerships 
            or start anew. My guess is that the outcome will be a mixture of the 
            two. <br>
<br>
<b>Partners depend on hard truth </b><br>
A final caveat is in order. There 
            is a misconception that partnerships are all warm, fuzzy, soft, 
            touchy-feely affairs. Nothing could be further from the truth. Good 
            partners should be 'truth-tellers', partners who can provide that 
            invaluable advice. Good partners have arguments and conflict - and 
            they resolve them. <br>
<br>
They participate in negotiations, honestly but 
            firmly. Your partners are, above all else, your agents, your eyes 
            and ears, and your mirror. They will tell you the hard truth about 
            the brutality of competition. They will help you see and hear things 
            you could not or would not know about. Most of all, they will 
            reflect how you behave as a partner - and that may be the most 
            valuable aspect of partnerships. <br>
<br>
Whatever you do, don't take 
            partnership issues, decisions and choices lightly. Picking the wrong 
            partners or failing to find partners you can trust will lead you to 
            failure, and your business to its demise.<br>
<br>
Partnerships are powerful 
            strategic tools, and only become foolish clichés when fools manage 
            them! The talents, skills and experience that make up competitive 
            advantage are embedded in the people who work in organisations. In 
            the 21st century whoever chooses and keeps the best partners and the 
            best people will win in highly competitive global markets, 
            regardless of the tumultuous conditions that may exist. Partner...or 
            perish! The choice is yours - make it wisely. </font></p>

