            <p align="center"><font face="Verdana" size="4" color="#FF0000">Our 
            game plan is to go regional<br>
            </font><b><font face="Verdana" size="1"><br>
            </font><font face="Verdana" size="2">Sheikh AbdulMalik bin Abdullah 
            Al Khalili<br>
            Chairman, BankMuscat</font></b></p>
            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im38.jpg" align="left" hspace="5" width="150" height="192">With 
            a 52 per cent market share, wouldn't the post-merger BankMuscat 
            become a monopolistic entity in the country? </b><br>
            That word monopoly is something that we have been hearing a lot, and 
            it seems to be a concern of a lot of people. If you look at the 
            earlier situation in the Omani banking industry, there were a number 
            of small banks, and there was severe competition among these small 
            banks, which were chasing the same business, and were thus not able 
            to take advantage of genuine business opportunities. While we 
            strongly believe in competition, and there is no way that we'll try 
            and play a monopoly, it should be healthy competition. What we have 
            witnessed in the last few years in the Omani market is unhealthy 
            competition, with a lot of undercutting prevalent. While the 
            customers thought they would benefit from increased competition, 
            they actually suffered. <br>
            <br>
            On the contrary, this merger will result in an expansion of 
            services, which will ultimately benefit the country. People 
            witnessed the benefits when we merged with ComBank. This merger will 
            refresh the national economy and the banking sector will become 
            stronger. The bank's assets will grow, which will enable us to 
            provide additional services such as apportioning loans to big 
            projects, providing financial consultancy, and supporting 
            privatisation. A large corporation doesn't mean non-existence of 
            other corporations. If anything, we expect this merger to result in 
            far greater growth opportunities for other banks in the Sultanate.
            </font></p>
            <p><font face="Verdana" size="1"><b>Wouldn't the size of the 
            post-merger BankMuscat become unmanageable? </b><br>
            The merged entity will be of a reasonable size by GCC standards. 
            Across the border, there are banks that are five to ten times bigger 
            than us. We are still nowhere near the biggest banks in the region. 
            So, this size is definitely not unmanageable. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What were the 
            reasons behind this merger, and what impact is the merger expected 
            to have on the country's banking sector as a whole? <br>
            </b>The merger between BankMuscat and NBO will be executed for many 
            reasons: Post the impact of WTO and Basel II accord directives, one 
            will be looking at far greater competition from large regional and 
            multinational banks who will want to make their presence felt in the 
            Sultanate. Competition will no longer be restricted to local banks 
            alone. <br>
            <br>
            Cross-border banks are here to stay and one must think of broadening 
            one's horizons and thinking of taking on regional and international 
            competition as that is where the real challenge lies. Our enhanced 
            size will not only help us protect our market share and be better 
            equipped to meet the challenges that lie ahead, but also provide far 
            greater benefits to our customers and other stakeholders as we 
            invest increasing amounts in systems and infrastructure upgrades.
            </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Why has 
            BankMuscat chosen the M&amp;A route to growth rather than growing 
            organically? </b><br>
            BankMuscat has indeed grown organically, as is evident from the 
            impressive development witnessed by the bank in the last 4-5 years 
            after the merger with ComBank. However, healthy and continuous 
            organic growth is possible only in a growing market, in countries 
            that have a substantial population growth. Given the demographics of 
            Oman, and the fact that local banks in the country are focused on 
            the home market, it makes sense to look at both the models of growth 
            - i.e., organic as well as through M&amp;As. <br>
            <br>
            A few years ago, at the end of the last century, BankMuscat 
            underwent a change of strategy to achieve critical mass because we 
            believe that with Oman entering the WTO, the country's banks would 
            inevitably face foreign competition. The country and its banks need 
            to brace themselves to face the challenge from these stronger banks. 
            A number of small local banks will not be able to face that 
            challenge as well as what a few strong ones could.<br>
            <br>
            The vision was clear since the last four years as we aim to become 
            one of the best banks in the region. Ours is a bank interested in 
            merging for additional value with a presence in all the GCC 
            countries. This merger is part of our game plan to defend our home 
            turf on the one hand and to diversify our business on the other. 
            Today, we have a growing presence across the region and a strong 
            presence in India, which is one of the markets with the strongest 
            trade and business ties with the GCC. Our presence in India is 
            through the 33 per cent strategic stake we have in Centurion Bank, 
            with whom we merged our erstwhile India operations. <br>
            <br>
            We also have a representative office in the UAE, which we have been 
            hoping to convert into a full-fledged branch for a while now. This 
            particular move, the merger with NBO, gives us the opportunity to 
            make that a reality. The third is Bahrain, where we bought out the 
            operations of ABN Amro bank three years ago. Then, of course, are 
            NBO's operations in Egypt and our banking licence in Saudi Arabia. 
            We are also looking at Qatar. <br>
            <br>
            <b>What will be BankMuscat's next move? </b><br>
            Our next move will be the conversion of our Bahrain unit into an 
            independent branch, and use that to leverage opportunities in the 
            region. We will continue to look out for opportunities of growth in 
            the regional market. Our overall game plan is to go regional, but at 
            the same time, we want to be strong at home, and that is the 
            advantage that this merger will give us. <br>
            <br>
            BankMuscat will continue to carefully examine all organic and 
            non-organic growth opportunities that come our way in our quest to 
            become one of the foremost banks across the region by the turn of 
            the decade. We will continue to explore all quality opportunities 
            that help us grow in strength and stature and provide better value 
            to our valued family of stakeholders. <br>
            <br>
            While the focus will be on the local market, as per our plans, 
            around 25 per cent of the bank's revenues will come from business 
            abroad. We have practical plans of achieving that through our 
            business in Bahrain and India. We have a banking licence to operate 
            in Riyadh, Saudi Arabia. Following the merger, we will also 
            carefully review NBO's Egypt operations and how it fits into our 
            overall game plan. We will take a decision on how we want to move 
            ahead, thereafter. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Why did 
            BankMuscat choose NBO and not any other local or regional bank? </b>
            <br>
            NBO was chosen for a number of reasons - one, it was the first local 
            bank to be set up in the country and hence enjoys substantial brand 
            equity, it is also the second-largest bank in the country today. The 
            bank has a string branch network covering the length and breadth of 
            the country, a stable customer base and strong systems in place - 
            all of these factors were given due weightage when we were exploring 
            the possibility of a merger. <br>
            <br>
            Moreover NBO was chose for its ownership structure. Other than the 
            Sheikh Suhail Bahwan Group, which BankMuscat has excellent relations 
            with, the rest of the ownership is neutral and we are keen to remain 
            neutral. There was a voting undertaken before the final decision to 
            merge, and the approval was unanimous in both the banks. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What kinds of 
            pressures will this merger put on the management of the new entity?
            </b><br>
            While there are many benefits that we expect out of the merger with 
            NBO, there could be uncertainties as well. These will largely be the 
            result of any merger, as a merger is the coming together of two 
            completely different organisations, often with completely different 
            cultures and working styles. We will probably spend the better part 
            of the next year concentrating on integrating the people, 
            infrastructure and the best practices of these organisations. Of 
            course, in the long run, I think because of our experience with past 
            mergers, we will definitely be in an advantageous position. <br>
            <br>
            BankMuscat has certain strengths and so does NBO. We will obviously 
            identify weaknesses in both and weed them out and capitalise on the 
            combined strengths. It is an opportunity for us to analyse our 
            weaknesses and compliment our strengths with those of NBO's. In 
            fact, prior to the merger, NBO has been one of the strongest 
            competitors of BankMuscat - now with NBO on our side, we will have 
            that edge in the market. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Wouldn't merging 
            with NBO before the capital infusion by the Suhail Bahwan Group been 
            more profitable for BankMuscat? </b><br>
            The negotiations for the merger had been underway for a while, and 
            such things obviously take time. Now, the discussions are complete 
            and the merger is expected to take effect from January 1, 2005, 
            subject to shareholder and regulatory approvals. The coming period 
            will be full of planning and work in order to execute the merger as 
            per the plans.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Will there be 
            retrenchment of staff?</b> <br>
            There are 1,600 BankMuscat employees and around 1,200 NBO staff 
            members. Employees of both the organisations will be treated by the 
            BankMuscat management in exactly the same manner. Staff members of 
            either bank need not worry about any negative impact on them. We are 
            extremely sensitive and cautious on handling our human assets, and 
            retaining and nurturing this talent is our topmost priority and the 
            biggest concern of the board. </font></p>
            <p><font face="Verdana" size="1"><b>What will be the composition of 
            the new board? </b><br>
            This is an issue that we'll address at the right time. </font></p>
