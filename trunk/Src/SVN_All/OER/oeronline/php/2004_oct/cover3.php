            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im40.jpg" align="left" hspace="5" width="150" height="195">What 
            impact will the merger have on the competitiveness of banks in the 
            country? </b><br>
            No doubt, size matters in banking. The new bank that emerges 
            following the completion of the merger will clearly have some 
            advantages over the other banks in Oman. Till now, no bank in Oman 
            has had enough underwriting capability to be considered seriously as 
            a Lead Mandated Arranging Bank for the large project finance deals 
            that have arisen in Oman. It will be refreshing to see an Omani bank 
            at the forefront of some of the more important local financings. <br>
            <br>
            On the local retail level, bigger is not necessarily better unless a 
            service culture permeates the entire branch network. Clients are not 
            likely to migrate to the new bank just because it is the largest. 
            Retail customers demand good service and a broad range of products 
            that satisfies their expectations. I firmly believe that every bank 
            in Oman has the ability and the opportunity to compete effectively 
            in this market.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Is OIB planning 
            to join hands with other local banks to ward off another acquisition 
            attempt by BankMuscat, or would it be open to joining the larger 
            entity?</b><br>
            This is not the first merger among banks in the Omani banking sector 
            and it may well not be the last. There is always a &quot;digestion&quot; 
            period during and following a merger. I don't think that BankMuscat 
            will be interested to consider any further mergers until this one is 
            properly &quot;digested&quot;. As far as OIB is concerned, we will continue to 
            focus on our clients and their needs. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What could the 
            scenario be post Basel II Accord coming into force? </b><br>
            I am not at all sure that we will see a common GCC market coming 
            into force anytime soon. I believe that Omani banks have a role to 
            play at the regional level and that role is likely to be expanded 
            somewhat as a result of this merger. However, our first priority as 
            Omani banks is to our local market and our clients here. I don't see 
            that changing in the foreseeable future. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What would be 
            the next logical step for BankMuscat?</b> <br>
            The next logical step for BankMuscat is introspection. I can't 
            imagine that any further local or regional mergers would be 
            contemplated by BankMuscat until all the implications of this one 
            are realised, addressed and absorbed.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Is the merger 
            expected to have any significant impact on the country's banking &amp; 
            finance sector? </b><br>
