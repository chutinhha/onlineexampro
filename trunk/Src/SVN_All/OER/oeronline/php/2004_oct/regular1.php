<p align="justify"><font face="Verdana" size="4" color="#FF0000">Banking on 
investments</font><font face="Verdana" size="1"><br>
<br>
<b>Ali Hassan Moosa provides an overview of the investment banking scenario in 
Oman and gives a lowdown on how and where to invest your dormant cash pile</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im30.jpg" align="left" width="125" height="147">Investment banking activities 
cover a wide array of financial services such as underwriting, corporate, 
project and structured finance including syndication, investment management, 
capital markets, securities trading, institutional equity, private clients 
advisory, derivatives, and mergers and acquisitions. <br>
<br>
The origin of investment banking dates back to an era between 17th and 19th 
Century to a number of European merchant families such as Barings, Rothschilds, 
Schroders and later on Warburg in the 20th Century. Thereafter, US economic 
dominance started to appear and names such as JP Morgan, Goldman Sachs, Morgan 
Stanley and Merrill Lynch took centre stage as the world's top investment 
bankers. <br>
<br>
Investment bankers usually perform four basic functions: <br>
(1) Arrange capital for institutions by underwriting and distributing new issues 
of securities;<br>
(2) Maintain markets in securities by trading and executing orders in secondary 
market transactions; <br>
(3) Provide advice on the issuance, purchase, and sale of securities, and on 
other financial matters; and <br>
(4) Create and manage collective investment vehicles. <br>
<br>
Investment bankers also solicit new untapped funds from large institutions and 
wealthy individuals on a case-by-case basis to finance transactions they 
arrange, instead of drawing on the existing pool of funds from commercial banks. 
Investment banks that also run investment management businesses and stockbroking 
businesses have to be careful to maintain strict 'firewalls' between both 
functions to ensure that price sensitive information does not reach these parts 
of the business before it is announced to the market as a whole. <br>
<br>
The danger is when research analysts and traders within the same institution 
collectively conspire to manipulate the market. In practice, this requires 
compliance with ethical and regulatory codes of conduct to avoid conflicts of 
interest, a source of constant challenge to the industry. <br>
<br>
Increasingly, investment banking in the developed economies of the west is 
migrating in two distinctly different directions: one, the tendency for global 
banking entities to merge and grow larger to reap the global scale economies 
created by falling capital controls; and two, the emergence of small niche 
industry or regional entities that fulfill specialist tasks.<br>
<br>
The Internet explosion and other technological advances have considerably 
affected investment banking. On one hand, investment bankers can get instant 
access to loads of information helping them take more informed decisions; on the 
other hand, it has eroded brokerage margins as online trading facilities charge 
lower commissions, making them widely popular. Market analysts estimate that 
online trading volumes are likely to reach US$ 5 trillion in the year 2005. <br>
<br>
<b>Investment bankers in Oman </b><br>
There are no 'pure' investment banks here in the Sultanate, but commercial banks 
who are involved to certain degree in investment banking activities. A number of 
local banks have established investment banking, asset management, and brokerage 
divisions/subsidiaries to manage various aspects of investments. <br>
<br>
Banks in Oman, like many other local and international investors, invest 
themselves for liquidity, yield and, of course, security. Bank investment 
portfolios consist of three categories (as per international accounting 
standards): originated by the bank, held-for-trading, and available-for-sale. 
Typical investment options include government development bonds, other foreign 
government securities, certificates of deposits, treasury bills, listed and 
unlisted securities. <br>
<br>
Bank investments are not a function of investment banking, but usually of 
treasury. Bank treasurers operate within the guidelines defined by the bank's 
Asset and Liability Committee (ALCO). This committee sets risk/ reward 
parameters that manage the investment mix, yield returns, and liquidity. <br>
<br>
Investment instruments issued by the Central Bank of Oman (CBO) are of three 
types:<br>
(a) Certificates of Deposits (CDs) that have a maturity of 28 days. <br>
(b) Treasury Bills (T-bills) that have multiple maturities of 91, 182 and 364 
days. <br>
(c) Government Development Bonds (GDBs) with long-term maturities last 3 to 5 
years.<br>
<br>
All of these instruments provide reasonable yields, but more importantly, are 
used as a defensive mechanism when liquidity is tight. All of the three 
instruments are eligible for repo (a mechanism used for borrowing short-term 
money) in the local inter-bank money market and the CBO. CDs and T-bills can 
also be bought back by the CBO at discounted rates and GDBs can be traded at the 
MSM. <br>
<br>
<b>Market moves </b><br>
The consolidated investment securities for six local Omani banks (Alliance 
Housing Bank, BankDhofar, BankMuscat, National Bank of Oman, Oman Arab Bank, and 
Oman International Bank) was RO251.8 million as of 2003 year-end, representing a 
respectable 8.8 per cent growth from the previous year. <br>
<br>
On the other hand, total assets of these banks stood at RO3.9 billion as of 2003 
year-end representing a growth of 3.8 per cent compared to the previous year. 
Two specialised government-owned banks--Oman Housing Bank and Oman Development 
Bank--were excluded from this analysis. <br>
<br>
The consolidated investment securities for eight international bank branches 
operating in the Sultanate (Bank of Baroda, Bank Melli Iran, Bank Saderat Iran, 
Banque Banorabe, Habib Bank Ltd, HSBC Middle East, National Bank of Abu Dhabi, 
and Standard Chartered Bank) was RO34 million as of 2003 year-end, representing 
a staggering 100 per cent growth from the previous year. <br>
<br>
On the other hand, total assets of these banks stood at RO537.5 million as of 
2003 year-end, representing an impressive growth of 13.2 per cent compared to 
the previous year. The State Bank of India's Oman branch was excluded from this 
analysis as it started operating in the year 2004. <br>
<br>
With the local banks, investment securities as a percentage of total assets 
differed widely from one bank to another as the percentage ranged from 4 to 15 
per cent, but averaged out to 6.3 per cent among the six banks. Similarly, with 
international bank branches, investment securities as a percentage of total 
assets differed widely from one bank to another as the percentage ranged from 
zero to 8 per cent, but once again, averaged out to 6.3 per cent among the eight 
banks. <br>
<br>
The average percentage of 6.3 per cent investment securities to total assets is 
rather small compared to international norms. The impediments for many banks 
against growing investment securities are the various restrictions imposed by 
local regulation on the type and investment mix. Concerned authorities may 
perhaps wish to review and amend such restrictions so that banks can further 
enhance their profitability and also the overall liquidity management of the 
organisations. <br>
<br>
<b>Regulatory environment </b><br>
The local banking regulator, the CBO, has set investment limits for banks in 
order to minimise risks and exposures associated with investment portfolios. 
Such limits include net worth ceiling of an overall 30 per cent on investing in 
GDBs, and 20 per cent on equity investments. <br>
<br>
Banks, however, cannot invest in any related party or in another bank without 
prior approval from the CBO. On the other hand, banks can invest up to 5 per 
cent of their net worth in any single company outside Oman and up to 5 per cent 
of shares of a single entity within Oman. <br>
<br>
Net worth, the criteria on which most investment limits are set, can be defined 
as capital plus retained earnings and reserves, or to put it simply, total 
assets less liabilities. It is therefore obvious that with the regulatory 
restrictions described above, certain banks may have already reached some of 
their investment limits or sub-limits, and thus, could lose out on 'windows' of 
opportunities with respect to sound investment prospects. <br>
<br>
Investment bankers and treasurers operate their businesses in volatile 
environments with factors like economic conditions, monetary and fiscal 
policies, the liquidity of local and global markets, international and regional 
political events, acts of war and terrorism, changes in laws and regulations 
governing practices, the competitive environment and investor sentiment.<br>
<br>
It is, therefore, natural for banks as far as investments are concerned to try 
and strike a healthy balance between security, liquidity and return Ð the latter 
in many instances being the lowest priority of all. <br>
