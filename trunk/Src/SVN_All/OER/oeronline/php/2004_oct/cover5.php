            <p align="center"><font face="Verdana" size="4" color="#FF0000">Oman 
            is ready to take on the region <br>
            </font><font face="Verdana" size="1"><br>
            </font><b><font face="Verdana" size="2">Mukhtar M. Hasan <br>
            Chartered Accountant </font></b></p>
            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im42.jpg" align="left" hspace="5" width="150" height="183">What impact will 
            the merger have on the competitiveness of banks in the country?<br>
            </b>I see the merger having a positive impact on the competitiveness 
            of banks in the country. BankMuscat has in the past come up with 
            innovative products for their customers in all areas including 
            consumer lending, investment funds, corporate bonds etc. I am 
            confident that it will continue to come up with new products that 
            will also encourage the remaining banks to enhance their service. 
            Moreover, many large projects in Oman were out of the reach of local 
            banks to participate in financing on their own due to their small 
            size. After this merger, BankMuscat will be able to compete with 
            other regional and international banks who come to Oman to look at 
            financing these large projects. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What, according 
            to you, are the reasons behind this merger? </b><br>
            The main reason behind the merger from the point of view of 
            BankMuscat is the fact that they have a stated objective of becoming 
            a leading regional bank. They must have seen this as an opportunity 
            to get into the Egyptian and the UAE markets in one swoop and 
            increasing the size of their balance sheet to enable them to compete 
            more effectively with other international institutions. From the 
            point of view of the NBO shareholders, I feel that this is a wise 
            move. Due to the recent problems NBO has had with non-performing 
            loans and provisions, the shareholders could probably not foresee a 
            dividend or return on their investment at least for another two 
            years or more. With this merger, they should start getting returns 
            by way of dividends and interest on bonds more or less immediately.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>How do you think 
            the rest of the local banks will react to this merger? </b><br>
            I do not see the rest of the three local banks-OIB, Oman Arab Bank 
            and BankDhofar-trying to join hands among themselves to ward off 
            being swallowed up by BankMuscat. Each of these banks is quite large 
            in the local market and is performing well. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Do Omani banks 
            have a chance to compete at the regional level once a common GCC 
            market comes into force? </b><br>
            I think the Central Bank of Oman is working very effectively with 
            the local banks to ensure that they are able to meet the Basel II 
            Accord requirements by 2005. The larger Omani banks will certainly 
            have a chance to compete at the regional level once a common GCC 
            market becomes a reality. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Do you think 
            there will be any perceptible impact on the end-consumer? </b><br>
            Yes, I think that there will be a favourable impact on the 
            end-consumer. I think that BankMuscat will continue to introduce 
            innovative new products into the market, which will also encourage 
            other banks to do the same. This will result in a favourable impact 
            for the end-consumer. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Does this merger 
            actually give enough muscle to the combined entity to be come a 
            serious regional player?</b><br>
            The merger does enable BankMuscat to become a serious regional 
            player, and I believe they will certainly stand a good chance 
            competing in the GCC markets. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Will the merger 
            have any significant impact on the country's economy as a whole, and 
            on its banking &amp; finance sector in particular?</b><br>
            Yes. The weak numbers coming from NBO over the last two or three 
            years were dragging the overall performance numbers of the Omani 
            banking sector. Eliminating one weak bank from the system will 
            definitely have a positive impact on the overall banking industry 
            numbers. Moreover, the country's economy as a whole will also 
            benefit as a larger bank will be able to fund larger projects that 
            are currently being undertaken in the country, which will result in 
            retaining the revenue within our economy rather than that revenue 
            being exported if the project were awarded to a regional or 
            international bank. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>What would be 
            the next logical step for BankMuscat?</b><br>
            As we are all aware, this merger is quite sizeable. The next logical 
            step for BankMuscat in my opinion would be to digest this merger and 
            consolidate their position over the coming period before they start 
            looking at further mergers or acquisitions within the country or the 
            region.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Are there any 
            niches that Omani banks may look at occupying after a common GCC 
            market becomes a reality?</b><br>
            I think that each of the Omani banks have their own core 
            competencies and niches where they perform well. After a common GCC 
            market becomes a reality, I would assume that the local banks would 
            like to exploit the larger market available to them in the areas of 
            their own core competencies. </font></p>
            <p><font face="Verdana" size="1"><b>Can you briefly provide a SWOT 
            analysis of the Omani banking sector?</b> <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">Strengths: 
            Well-regulated, good information database and sharing. <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">Weaknesses: 
            Small size of banks, reliance on consumer loans. <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">Opportunities: 
            Common GCC market will give them a larger market to operate in. <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">Threats: 
            Common GCC market will also bring in competitors into the country.</font></p>
