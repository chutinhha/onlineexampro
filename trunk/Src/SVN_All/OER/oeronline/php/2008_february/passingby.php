<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Creating Waves</font></b><font face="Verdana" size="1"><br>
<b>Alexandros N. Tombazis, Architect, Meletitiki - Alexandros N Tombazis and 
Associates Architects Ltd., from Greece was in Muscat in January. The acclaimed 
architect is designing the stylish and contemporary villas in the Wave, Muscat. 
OER caught up with him to know more about him</b><br>
<br>
<b>
<img border="0" src="passingby.jpg" width="175" height="184" align="left" hspace="8" vspace="6">Tell us more about yourself?</b><br>
I finished architecture at the National Technical University of Athens, with 
honours. I have also worked as an assistant at the National Technical University 
of Athens. As Principal of Alexandros N. Tombazis and Associates Architects, I 
have taken part as a member of different juries of international and national 
architectural competitions. For instance: E.C. DG XII Competition, EUROPAN 
Competitions, PLEA Competitions etc. I am also a member of different committees 
related to the use of alternative energy sources and bioclimatic architectural 
design. There are quite a number of references on my work in numerous books and 
magazines in Greece as well as abroad.<br>
<br>
<b>What brings you to Oman? </b><br>
I have been invited by the Wave to develop unique villas and hotels. I am 
extremely honoured to take up this task. Having five decades of experience in 
architecture, I am sure that this project will also make an indelible mark.<br>
<br>
<b>What is so unique about your work for the Wave, Muscat?</b><br>
What we have done in the villas up to now is first of all to design not for 
ourselves, but in turn think about the future users and what their requirements 
are. In architecture, one should not design for oneself. Rather step into the 
customer’s shoes and design accordingly, which means designing with the climate 
and not against the climate. My role is to provide sufficient shading, agreeable 
outdoor space and enough space inside the house. <br>
<br>
<b>If we look at global trends, how is your design going to be different?<br>
</b>In the history of architecture, even in the first half of 20th century, 
there were some rules within which the practitioners of architecture used to 
follow judiciously. Today these rules are much more relaxed and I think it is 
very good in my point of view. So you have to think through straightjacket. We 
have to get inspiration from vernacular architecture, but I think we should not 
replicate it. Rather, we have to imbibe those designs and incorporate it in 
contemporary style. In former times, our forefathers had limited means so they 
built houses that ensured that all utility needs are met. Today, we have 
unlimited means and there is no dearth for technology. That is why projects like 
The Wave are taking shape and people have more disposable income to lead a 
luxurious life.<br>
<br>
<b>What are the other major projects that you have undertaken recently?</b><br>
I was teaching in the school of architecture in Athens, Greece for the past 50 
years. So I have had the opportunity to design major projects. I have had the 
opportunity to take part in the tourism development in South West Greece. We 
developed a hotel of about nearly 3,000 beds. We developed two hotels with spa 
and all other modern amenities, named as Navarino. We also developed properties 
in Romania, Cyprus and Greece. We also built a mosque in Al Nasar Square in 
Dubai, UAE that was inaugurated last October. Besides that we built a big 
church, called Fatima Church in Portugal, where 9,000 people can sit at a time. 
This place attracts more than one and a half billion visitors today.
