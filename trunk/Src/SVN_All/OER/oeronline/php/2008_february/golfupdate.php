</font><b><font face="Verdana" size="4">Get set for Saudi Masters</font></b><font face="Verdana" size="1"><br>
<b>Saudi Arabia is getting ready for its first international golf tourney at the 
Dirab course</b><br>
<br>
<img border="0" src="golf.jpg" width="250" height="164" align="left" hspace="8" vspace="6">The Kingdom of Saudi Arabia is hosting its first European PGA Tour golf 
tournament, with the Saudi Masters being staged at the Dirab Golf Club near 
Riyadh from March 19-21. Earlier, the European Seniors Tour had to be held in 
Bahrain and Abu Dhabi in the GCC region. The main European PGA Tour also has a 
long history of association with Abu Dhabi, Dubai and Qatar. This is the first 
time that Saudi Arabia is hosting an event with the PGA European Tour Group. <br>
<br>
Around 72 professional golfers, including players such as Carl Mason, winner of 
the 2004, 2005 and 2007 European Seniors Tour Order of Merit, Ian Woosnam and 
Sam Torrance, both multiple tournament winners on the European Tour and winning 
Ryder Cup captains, are expected to take part in the tourney. Six regional 
amateur players will supplement the international field. <br>
<br>
Built in 1991 by Khaled and Mohammed Abunayyan, the Dirab Golf and Recreation 
Centre is renowned for its exceptional year-round playing conditions, carved 
rolling fairways, large target greens and challenging hazards. The club is 
located 40 km southwest of Riyadh, nestled in the picturesque Dirab Valley. The 
Centre shows the growth the game has registered in the Kingdom since American 
expatriates first introduced golf to Saudi Arabia in the late 1940s when they 
created a course near Dhahran. <br>
<br>
Dirab Golf Club boasts a 7,280-yard par-72 championship course, featuring lush 
tree-lined fairways set against the backdrop of the Twaiq Mountains. The 
practice facility at the club is a destination that is both a challenge and fun. 
The anchor of the practice area is a wide driving range 27,000 sq.m long, 
excluding the 1,600 sq.m practice tee, with all-grass hitting stations that can 
simultaneously accommodate 30 golfers, with plenty of room for long hitters to 
practice their 300 yard drives.<br>
<br>
The club is replete with 12 synthetic mat stations with automated ball 
dispensers with sunshades, enabling golfers to stay cool during their practice. 
The hitting stations are lit after dark. The distance flags are located just off 
the driving range to help you determine which club and swing length to use for 
each distance. Grass stations are rotated frequently to ensure a quality 
practice surface. Grass range maintenance includes weekly aeration, seeding and 
top dressing.<br>
<br>
The pitching area, consisting of both a pitching green and distance flags, is 
about 9,000 sq.m and allows players to pitch to a green from multiple situations 
such as from below the green, above the green, over bunkers, side hill lies and 
flat lies.<br>
<br>
The Dirab Pro Shop offers the latest models in drivers, irons, putters, wedges, 
gloves, shoes, bags, trolleys carts, balls and golf wear and accessories. 
Professionally trained employees can help both a beginner and a pro find the 
right equipment for the game. Club repair services like changing grips, lofting, 
lying alteration, etc., are also available on the course.<br>
<br>
The Dirab Golf Academy combines a new nine-hole par three course, situated near 
the 18th hole, with the opportunity to take Digital Video Golf Lessons. Using 
the latest golf technology tuition software, the Interactive Frontier V1 system, 
the Golf Academy lessons can offer features like recording all lessons using a 
digital camera, model a player’s swing to that of a touring golf pro, and even 
overlay one’s swing with the swing of a professional for comparison.<br>
