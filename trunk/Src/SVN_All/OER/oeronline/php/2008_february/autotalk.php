        </font><b><font face="Verdana" size="4">
        SEE 30:30</font></b><font face="Verdana" size="1"><br>
        <b>When was the last time you heard of a Volvo sports hatch?<br>
        The Swedes have apparently decided to strike back</b></font></p>
        <p align="center"><font face="Verdana" size="1">
        <img border="0" src="auto1.jpg"></font></p>
        <p align="justify"><font face="Verdana" size="1">
        Until now Volvo has been known for creating
the safest family transports on the planet.
But they almost always lacked excitement
and the young generation kept away. Now Volvo has
decided to shake things up a bit. With the release of
the new C30, they have squarely targeted the hearts
of youngsters. It is Volvo’s answer to the Renault
Clio sport and the Volkswagen GTi, and we were
happy to drive it.
<br>
        <br>
        <b>Exterior</b> <br>
        It seems that Volvo didn’t make much of an effort on
the front portion of the C30 which is similar to the
S40. It is handsome to look at but there is just nothing
new about it. But from the back and side, the differences
become very apparent. Volvo has really taken a
bold step and tried something new on the back, and it
works. Its wide wheel arches, chrome exhaust tip and
the all-glass lift gate really add to the C30’s already
sporty look. Also the car we drove came fitted with the
optional body styling kit, which thankfully is standard
in Oman. It really adds to the sporty character and
presence of the car. Overall it is a great looking car
but we really wish Volvo would have taken the even
bolder step of shaping the front of the car to differentiate
it from the rest of the Volvos.
<br>
        <br>
        <b>Interior</b> <br>
        The C30 shares its interior styling with the S40 and
the S60. It is well designed with practicality and
ease of use in mind. Even the floating dashboard
works beautifully with the interior theme and all controls
are exactly where you would expect them to be.
The C30’s high quality plastics and fabric are of the
highest quality but the buttons on the dash do feel
a little bit tacky. Overall the quality of the interior is
good enough to rival even the best of its German
counterparts.
<br>
        <br>
        Volvo has been very generous with the interior space:
there is plenty of head and legroom for the front passengers.
But a large boot has been achieved at the
expense of the rear seat space. Another drawback
is that the rear seats are individual without a centre
seatbelt – not a bench seat – removing the possibility
of putting a third person in the back, not that he/she
would fit in the back in the first place. Overall, the
interior is great as long as you plan on carrying no
more than two adults and two kids with you.
<br>
        <br>
        <b>Practicality</b> <br>
        The C30 is optimally equipped. It has everything you
would expect a car of this type to have but nothing
particularly special. It is well equipped with options
such as a CD player, auto A/C and power windows
as standard. But when it comes down to safety,
Volvo rules. It has everything from airbags to well
positioned crumple zones. And in proper Volvo tradition,
we are quite sure, even the ashtray is pushed
out of the way in case of a collision. Suffice to say
there is no safer way to get from A to B than in the
C30 – that is until you opt to pick up a higher end
Volvo or something the size of a Hummer H1.
<br>
        <br>
        <b>Drive</b> <br>
        The C30 is a huge improvement over all other Volvos
when it comes down to sheer driving pleasure. The
steering is sharp and the handling is precise thanks
to its Ford Focus-shared underpinnings. The steering
does feel a little numb and the ride a tad-bit hard
but this is a very small price to pay for such a well
behaved car. As far as performance is concerned,
the car we drove certainly delivered. The C30 can
hit 100kmph from standstill in a mere 8.8 seconds
and up to a max speed of 215kmph. This is thanks
in part to its low mass and a very smooth 170hp
2.4 litre five cylinder engine. Volvo has even done
a great job of programming the auto gearbox. The
shifts are precise and quick, there is no hint of delay
as long as you don’t try to shift the gears manually
– in tiptronic mode, the gearing is more relaxed and
downshifts are slower than expected, it is best left in
auto allowing the computer to shift.
<br>
        <br>
        In the end, the C30 is an enjoyable car to drive,
the handling is great and there is performance in
abundance. If Volvo added some excitement to the
steering and improved the tiptronic response, the
C30 would have been perfect.
<br>
        <br>
        <b>Summary</b> <br>
        The C30 is a great car; it handles great and is well
behaved. The equipment level is good and it is also
extremely safe to drive. It is more expensive than its
rivals but you do get what you pay for. And if Volvo
makes a little extra effort, the C30 will be near perfect,
but as it stands, it is the kind of car an executive
bachelor – who wants exclusivity and a fun drive but
nothing too raw – would buy.</font></p>
