<b>Omantel Research Abstract
<br>
</b>Omantel’s business fundamentals remain strong,
with a growth likely in its mobile subscriber base,
monopoly in fixed line internet market and a healthy
balance sheet. Omantel’s Q3 results for 2007 were
above par mainly due to favourable government
ruling and partially due to strong performance in
interconnection revenue. The company reported
broad-based growth, with mobile segment revenue
increasing 12 per cent year on year (YoY) while
fixed line revenue rose 13 per cent YoY. The major
highlight was the announcement of ‘World Call’ acquisition,
which is expected to garner revenue and
be accretive to earnings by FY09.<br>
<br>
Departing from its traditional focus on local business,
the company recently ventured into international market,
with the acquisition of Pakistan-based World Call
Telecom Company (WCTC). From a long-term business
perspective, it is encouraging to see the company
management’s pursuit to diversify its revenue stream while extenuating country 
specific risk. The
WCTC acquisition, though expensive at 18 times its
expected FY08 earnings, is believed to enhance the
current technological know how of Omantel to the next
level of bundled services. Furthermore, the company
has a healthy balance sheet and strong cash flows to
expand even further, inorganically.
<br>
<br>
Oman’s telecom industry is currently witnessing a
transformation, with Government involvement decreasing
even as competition in the technological
and pricing space is rapidly increasing. Mobile technology
has been the front-runner in the advancement
of the communication technology in the region and
the future is expected to be broadband business. The
Government is supporting the cause for radical upgrading
of communication and information technology
in the region, which we believe will open vistas for
both the telecom companies, Omantel and Nawras.
On the back of favourable ruling from Oman government,
the company reported 36 per cent YoY rise in
net income and net margin expansion of 540 basis
points YoY to 31 per cent, for the first nine months of
2007. In its pursuit to develop the country’s mobile
market and reduce tariff rates, the Oman Government
reduced royalty charges (with retrospective effect
from January 1, 2007) on both the telecom companies,
Omantel and Nawras. The government reduced
royalty rates to 7 per cent on both fixed-lined revenue
and mobile network revenue from previous rates of 10
per cent and 12 per cent, respectively.
<br>
<br>
Based on its multiple valuation methodology, Vision
Securities has arrived at a weighted average target
price of RO2.050 for Omantel. This factors in the
company’s ability to generate stable-free cash flow
and provide shareholders with reasonable dividend
yield. It has also used comparative valuation to
highlight the company’s performance compared to
its peers in the region. The growth in other regional
players has been due to their international exposure
and capability to monetise the expansion. Hence,
lower EV/EBITDA and P/E multiples have been assigned
to Omantel compared to its peers. According
to Vision’s research, earnings growth from current
operations is reflective in the market price though
it would be interesting to watch out on the earnings
additions from new acquisitions as well as data revenue
from 3G launch.
<br>
<br>
On the economic front, Ministry of Finance presented
its annual review of the Omani economy for
2007, and also the annual budget for the year 2008.
A RO400 million-deficit budget was announced,
with a conservative oil price estimate of US$45
per barrel. Signalling a tighter monetary policy, the
Central Bank of Oman (CBO) raised commercial
banks reserve requirements (CRR) from 3 per cent
to 5 per cent of the total deposits, owing to the rising
liquidity in the monetary system.</font><i><font face="Verdana" size="1"><br>
