</font><font face="Verdana"><font size="4"><b>Qatar leads GCC markets</b></font><font size="1"><br>
<b>The year-end was positive for all the GCC markets. In Oman, the 2008 budget 
has further improved sentiments<br>
</b><br>
</font></font><font face="Verdana" size="1">The MSM continued its upward momentum
during the period December 16, 2007, to January
15, 2008, rising 8.8 per cent after testing
all-time highs of 9908 in early of January. However,
the Benchmark MSM-30 consolidated in the latter
half of December 2007, showing signs of cooling
off. The banking and investment index outperformed
(up 8.8 per cent) the other indices; industry index
was up by 5 per cent and service and insurance index
10.2 per cent. The market was buoyed by high
liquidity and increasing presence of foreign investors.
Daily average turnover increased by a whopping
6-7 times when compared to similar period in
2006, confirming strong liquidity trends.
<br>
&nbsp;</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">In corporate developments, OIFC announced that it
had been appointed as one of the agents for the distribution
of starter kits and prepaid cards by Oman
Mobile for a period of two years, with effect from
January 2008. OFIC reported that it expected to earn
reasonable profit from this distribution agreement.
<br>
<br>
In another development, Oman Oil Co., the parent
company of Oman Oil Marketing Co., after further
review decided not to dispose of its shareholding in
the latter, as earlier planned. <br>
<br>
<b>GCC MARKET COMMENTARY
<br>
</b>All GCC markets ended the one-month period ending
January 15 on a positive note. While the Qatari
Market registered the biggest gain at 13.3 per cent,

the Dubai Financial market (DFM) followed with
9.7 per cent. Abundant liquidity in the GCC financial
system was attributed to be the key reason for
such sharp gains. Investors in Saudi Arabia were
positive about the annual budget for 2008, and for
the full-year earnings for 2007. Allowing more foreign
participation has been hinted at by the market
regulators in the Kingdom and that indeed would
be a significant positive. Dubai market was driven
mostly by the increased interest in Emaar Properties,
mainly on the news of market floatation of its
joint venture in India. Abu Dhabi market registered
a gain of 9 per cent, driven mostly by real estate
and energy stocks, while the Kuwait Stock Exchange
clocked in 8.5 per cent on higher dividend expectations.
Bahrain market was pushed up by 5.6 per cent
by banking and investment stocks, whereas Saudi
Arabia’s Tadawul All Stock Index (TASI) registered
a gain of 7.9 per cent.
