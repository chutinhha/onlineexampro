        </font><b><font face="Verdana" size="4">
        RO400 Mn deficit! Relax</font></b><font face="Verdana" size="1"><br>
        </font><b><font face="Verdana" size="1">Sovereign Wealth Funds or SWFs 
        are making the talking point across financial capitals. Pension Fund of 
        Oman and Oman Investment Fund with undisclosed reserves scouting around 
        the globe for suitable investment opportunities to park their funds to 
        generate ‘super’ returns is a case in point</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="investment.jpg" width="130" height="207" align="left" hspace="8" vspace="5">The Sultanate of Oman 2008 general budget is
out. Minister of National Economy HE Ahmed
bin Abdulnabi Macki has chalked out a RO400
million deficit budget for the current year. Despite
the global oil price ruling around US$90 a barrel and
Oman’s own crude selling
around US$65.15 a barrel
on an average, the government
has based its budget
estimates on US$45 a barrel.
A calculated and conservative
move, indeed.
<br>
<br>
A RO400 million budget
deficit is no big deal out of
RO5.4 billion expected revenue
stream. The forecast
deficit will hardly matter
when the toted up expenditure
is pegged RO5.8 billion.
This projected shortfall
constitutes three per cent of
GDP or seven per cent of projected revenue. Macki
is unfazed thanks to the huge General Reserve Fund
that the government has been building up for years
through taking lesser credit through oil export revenue
than actually warrants.
<br>
<br>
That brings us to the core issue. It is just not the
General Reserve Funds are lying idle. Macki, wearing
the alternate hat as Deputy Chairman of the Financial
Affairs and Energy Resources Council, is deploying
the funds judiciously through investments overseas
as well. Sovereign Wealth Funds or SWFs are making making
the talking point across financial capitals. Pension
Fund of Oman and Oman Investment Fund with
undisclosed reserves scouting around the globe for
suitable investment opportunities to park their funds
to generate ‘super’ returns is a case in point.
<br>
<br>
<b>What is SWFs?</b> <br>
These funds broadly denote designated pools of
assets owned and managed by governments and
predominantly invested worldwide to attain higher
returns. A much simpler understanding about SWFs
is that it is carved out of foreign exchange reserves
when the country believes that its forex pool has
surplus and deploy it to accrue higher returns from
assets all across the world.
<br>
<br>
And such funds are operated by special designated
agencies. They have been in existence for more than
five decades now with the first SWF believed to have
been created by Kuwait in the form of the Kuwait Investment
Authority (KIA) in 1953. And this was followed
by creation of similar funds by the UAE, Oman,
Singapore, Brunei, Norway, Malaysia, Singapore, Australia,
Chile, Veneuzuela, Russia, China, etc all through
between 1960 to the early part of this decade.
<br>
<br>
The UAE’s Abu Dhabi Investment Authority Fund
(ADIA) is considered to be the world’s largest SWF
currently with an Asset Under Management (AUM)
figure of a staggering US$625 billion. Another
prominent SWF which has been much in news globally
for quite some time has been Temasek Holdings
owned by the government of Singapore which
has an AUM of over US$100 billion. 
</font></p>
        <p align="center">
        <img border="0" src="investment1.jpg" width="400" height="301"></p>
        <p align="justify"><font face="Verdana" size="1"><b>The Middle East Surge
        </b><br>
        Though such funds have been in existence for quite
sometime, their big ticket shopping spree has started
becoming more ferocious only in recent years.
And the critical reason for this is while emerging
economies in Asia have been piling up foreign
exchange reserves after the end of Asian crisis towards
the concluding part of the previous decade,
the growth in the west has largely saturated. The
rising oil prices in the international markets have
also broadened the base of SWFs operated by the
countries in the Middle-East.
<br>
        <br>
        The fact that the appetite of the operators of these
funds is now increasingly getting to insatiable proportion
is well reflected by some of their recent
deals. According to a Morgan Stanley estimate,
between April and November last year SWFs bought
stakes in the leading financial companies of the
west (read the US and EU) worth $55 billion.
<br>
        <br>
        And some of the most buzzing moves of these funds
in the recent past have been: Morgan Stanley offloading
its 9.9 per cent stake to the China Investment
Corporation for US$5bn, and the Government
of Singapore Investment Corporation making a hefty
purchase worth 6.7 billion Euros of UBS’ convertible
bonds. The Abu Dhabi Investment Authority has
spent US$7.5 billion in buying convertible bonds
in Citigroup recently and Safe Investment of China
has bought less than one per cent stake in ANZ. And
this list is simply growing with each passing month
now changing track from occasional occurrence to
a regular affair.
<br>
        <br>
        <b>The Challenge
</b><br>
        In terms of cumulative value, these funds may pale
into insignificance to the organized financial structure
of the developed market as their worth is presently
slated to around US$3 trillion which can’t stand the
might of US$64 trillion held by top 500 asset management
companies of the world (nearly two-third of
them are from the developed countries).
<br>
        <br>
        However, what can’t be ignored is that SWF corpus
has multiplied at a break-neck speed in last ten years
– from around $600 billion in 1997 to the present
level now. And analysts see their swelling spell to
grow more robust in the near future. Gerard Lyons,
chief economist at Standard Chartered Bank recently
remarked, “Sovereign wealth funds’ influence on financial
markets is set to grow. From the present level,
they could reach to $13.4 trillion in a decade. The
way they operate is how fund management should be:
if you can take a long-term view, there are good bets
you can put on, regardless of short-term volatility.
They are the new power brokers.”
<br>
        <br>
        These funds have to return home at point of time
or other. Merrill Lynch’s latest report by Economist
Alex Patelis is bang on target: “Recall that SWF investments
are in overseas markets; as these investments
mature and generate returns, there will be an
increasing temptation by governments to repatriate
part of these funds to plug potential holes in domestic
budget deficits.”
<br>
        <br>
        The reports draws attention to what happened in
Norway. “Even though the government has imposed
certain restrictions. In a sense, SWFs can also act as
‘automatic stabilisers’ to domestic economies, shielding
them from sharp downturns and elongating business
cycles. This also supports our view that the global
economy remains in good shape. In any case, investments
by SWFs are made to be able to finance future
consumption. At some point – albeit decades from
now – repatriation of SWF funds back into their home
countries is likely to put significant upward pressure on
their currencies and boost their economies further.”
<br>
        <br>
        SWFs are trying to balance several objectives with
their investments: increase returns, achieve greater
portfolio diversification but also focus on national 
strategic objectives. In recent weeks it has become
increasingly evident that these strategic objectives
feature prominently in many of the announced deals.
For example, government controlled CITIC Securities’
deal with Bear Stearns also featured an agreement to
start a Hong Kong-based joint venture offering financial
services in other Asian markets. Qatar Investment
Authority and Borse Dubai’s investments in the London
Stock Exchange presumably also support their efforts
to develop capital markets in their home countries.
ICBC’s investment in Standard Chartered allows China
to develop a foothold in Africa. It also underscores another
point often lost: that investments need not take
place directly out of SWF funds, as the latter often have
controlling stakes in banks that act as primary agents.
<br>
        <br>
        Returning to Oman, Macki’s objects are no different.
He has to allocate RO725 million – 45 per cent more
than the previous year – for developmental projects
this year. Defence and security allocations have to
be upped, whether you like it or not. Don’t forget the
social sector spending – education, healthcare etc.
Yet, Macki shows no sign of worries. He need not
and you know why.</font></p></td>
