Tracking the rise<br>
</font></b><font face="Verdana" size="1">
<b>The Dow Jones-MSM Index, launched in January, will enhance Oman’s investment 
appeal for international investors</b><br>
<br>
<img border="0" src="news2.jpg" width="175" height="85" align="left" hspace="6" vspace="4">The launch of the Muscat Securities Market (MSM) and Dow Jones Indexes 
co-branded ‘Dow Jones-MSM Index’ on January 15 in Muscat signals a new effort by 
the authorities to enhance Oman’s international appeal as a potential market to 
invest. <br>
<br>
As per an agreement signed by the two sides in May last year, Dow Jones Indexes 
will draw up a family strategy covering the development of indexes, notably the 
Dow Jones-MSM Composite Index and the Dow Jones-MSM Blue Chips Index. The 
co-branded index is expected to help raise Oman’s profile through the MSM. <br>
<br>
According to experts, Oman’s booming economy and well-regulated market offers a 
unique investment opportunity. According to Dow Jones, ETFs are the most 
innovative class of securities to hit world markets since the introduction of 
mutual funds. ETFs are securities that closely resemble index funds, but can be 
traded like stock. Unlike most mutual funds, ETFs provide diversified investment 
opportunities for investors through open-ended collective schemes that can be 
traded at any time throughout the course of the day. Essentially, they offer the 
convenience of a stock along with the diversification of a mutual fund. <br>
<br>
The MSM is looking ahead to encourage the listing and trading of such new 
investment tools to satisfy the needs and demands of local and foreign 
investors, and to spread and minimise risk, which is a fundamental element for 
investment decision-making. <br>
<br>
<b>Well-defined parameters<br>
</b>The Dow Jones-MSM Index currently contains 66 securities. The universe for 
the Index is defined as all companies listed on the MSM that trade at least 10 
days in a quarter. Companies that trade less than seven days in a quarter will 
be removed from the index. Currently, the top five companies in the index by 
free float market capitalisation are: Raysut Cement Co., National Bank of Oman, 
Bank Muscat, Oman Telecommunications Co. and Renaissance Services SAOG.<br>
<br>
The Dow Jones MSM Index, which is calculated in Omani Rial (RO), will be 
reviewed quarterly in March, June, September and December. The index is weighted 
by free-float market capitalisation. Weights of individual stocks are capped at 
10 per cent in order to prevent the index from being dominated by certain 
securities. In 2007, the Dow Jones-MSM Index gained 72.73 per cent. <br>
<br>
A full-service index provider, Dow Jones Indexes develops, maintains and 
licences indexes for use as benchmarks and as the basis of investment products. 
Best known for the Dow Jones Industrial Average, Dow Jones Indexes also is the 
co-owner of the Dow Jones Stoxx Indexes, the world’s leading pan-European 
indexes, and together with Wilshire Associates, provides the Dow Jones Wilshire 
Global Index family, which is anchored by the Dow Jones Wilshire 5000 and covers 
more than 12,000 securities in 59 markets. Beyond equity indexes, Dow Jones 
Indexes maintains a number of alternative indexes, including measures of the 
hedge funds and commodity markets. Dow Jones Indexes are maintained according to 
clear, unbiased and systematic methodologies that are fully integrated within 
index families.<br>
<br>
Dow Jones &amp; Company is a subsidiary of News Corporation and is listed on the 
NYSE: NWS, NWS.A; ASX: NWS, NWSLV stock exchanges. Dow Jones is a leading 
provider of global business news and information services. Its consumer media 
group publishes The Wall Street Journal, Barron’s, Market Watch and the Far 
Eastern Economic Review. <br>
<br>
Its Enterprise Media Group includes Dow Jones Newswires, Factiva, Dow Jones 
Client Solutions, Dow Jones Indexes and Dow Jones Financial Information 
Services. Its local media group operates community-based information franchises. 
Dow Jones owns 50 per cent of SmartMoney and 33 per cent of Stoxx Ltd, and 
further provides news content to radio stations in the US. </font></p>
