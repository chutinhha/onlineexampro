<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" size="4"><b>Dare to 
dream</b></font><font face="Verdana" size="1"><br>
<b>The rest of the world needs to know about the fast pace of tourism development 
in the GCC, especially Dubai, and learn how to make tourism happen despite 
environmental adversities. An OER report on tourism development and investment 
in the region</b></font></p>
<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<img border="0" src="realestate.jpg" width="400" height="443"></font></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">It is not easy to measure the size of the committed investments in the tourism 
sector in the region. The Tourism Development Projects &amp; Investments Market (TDIM), 
a trade show organised by Reed Exhibitions Middle East in Dubai from January 
20-22, which attracted more than 50 leading tourism project developers, has 
thrown up some contrasting figures.<br>
<br>
On the optimistic side, if we trust the figures thrown up by Fast Future and 
Global Futures and Foresight (GFF)’s ‘The Middle East Leisure Landscape 2020’ 
study, there are at least 166 projects with a combined value of more than US$1 
trillion across the MENA region. The study covered 13 countries, including 
Bahrain, Egypt, Iran, Jordan, Kuwait, Lebanon, Oman, Qatar, Saudi Arabia, Syria, 
Turkey, UAE and Yemen.<br>
<br>
“The sheer scale of the spending on travel and tourism across the Middle East 
demands an equivalent level of investment in the supporting leisure landscape, 
if the region is to succeed in attracting the visitor levels it is targeting. 
The research highlights that there is clearly the ambition, vision and funding 
in place to develop a range of world-class leisure offerings across the region,” 
says Rohit Talwar, the report’s co-author and CEO of Fast Future and GFF. <br>
<b> <br>
Mega-projects</b><br>
The three largest spending locations identified were Dubai with US$381.4 billion 
worth of projects either underway or planned, Saudi Arabia with initiatives 
valued at a total of US$184.4 billion and Abu Dhabi at US$131.3 billion in new 
projects. At the macro level, the biggest individual developer is Tatweer, with 
US$170 billion of projects, with Dubailand alone accounting for US$110 billion 
of this. Aldar is the second largest, with US$71.1 billion worth of developments 
in the pipeline. Emaar is developing US$16.5 billion of projects but if its 
stakes in joint ventures such as King Abdullah Economic City are considered, the 
total value of projects in which it is involved rises to US$136.5 billion.<br>
<br>
Of the US$1.006 trillion worth of developments identified, US$171.8 billion has 
been committed for pure leisure projects covering developments such as museums 
and theme parks. A further US$218.3 billion is being invested in development of 
leisure resorts. A total of US$611.9 billion worth of mixed-use initiatives have 
been identified, covering projects such as residential or commercial 
developments with a large leisure component. The largest individual projects 
identified included King Abdullah Economic City in Saudi Arabia valued at US$120 
billion, the US$110 billion Dubailand theme park, Kuwait’s US$86 billion City of 
Silk Development and the US$61 billion Arabian Canal Development in Dubai.<br>
<br>
“To compete for visitors’ attention, these projects will need to develop strong 
branding and clearly differentiated propositions. The scale of resources 
required to build and operate these facilities will place a huge demand on 
critical resources, such as water, and create major environmental pressures. 
Finally, the region is already facing the risk of project delays on existing 
developments due to a shortage of skilled workers. These new developments will 
only aggravate such problems. A particular concern is the growing shortage of 
project management talent capable of delivering these multi-million dollar 
developments. All three of these sustainability challenges will need to be 
addressed in a strategic manner if developers are to ensure the long-term 
viability of their investments,” says Talwar.<br>
<br>
<b>Investment flows</b><br>
Those who believe that the Fast Future and GFF figures are highly inflated may 
find Reed’s figures, released almost an year ago, to be on the more optimistic 
side. Reed restricted its research to projects with investments committed up to 
2018. According to Reed, “of the AED 1.2 trillion (US$333.3 billion approx) in 
investment commitment that the tourism projects sector has attracted in the MENA 
region, the GCC countries account for AED1 trillion (US$277.7 billion) or more 
than 85 per cent. The UAE is the dominant player, with an 85 per cent share of 
the total value of announced projects, amounting to AED858 billion (US$238.3 
billion). Of this, tourism projects in Dubai alone total up to over AED450 
billion (US$125 billion), or more than 50 per cent of the value of UAE 
projects.” It further reports, “Oman with AED61 billion (US$16.9 billion) in 
tourism project investments, and Qatar, with AED31.1 billion (US$8.6 billion), 
occupy the second and third slots among GCC countries.”<br>
<br>
Even though we may have to take the above two figures with a pinch of salt but 
the response at the TDIM08 indicates that tourism sector in the region is 
growing at a faster pace then anywhere else in the world. The show featured 
major developers and developments, including DTCM (Dubai Tourism &amp; Commerce 
Marketing), Nakheel, Dubailand Destination, Global Village, Falcon City of 
Wonders, etc., unveiling their latest offerings.<br>
<br>
While Nakheel unveiled its new ambitious projects The Universe, Blue Communities 
and the redevelopment of Port Rashid (see nex page), Tatweer, the developer of 
Dubailand, announced its tie-up with US production major DreamWorks Animation 
SKG (best known for its popular animation character Shrek) to develop a 
46.5-hectare theme park besides restaurants, hotels and retail outlets. 
Dubailand is the world’s most ambitious tourism, leisure and entertainment 
project, with an estimated investment of US$110 billion covering 3 billion sq ft 
area. It will have seven theme parks and at least 55 hotels catering to 15 
millions visitors a year.<br>
<br>
Al Ain Wildlife Park &amp; Resorts featured its unique mixed-use development, with 
hotels, five-star luxurious resorts, shopping malls, residential units, etc., 
within a themed wildlife park in Abu Dhabi. Bawadi prominently displayed its 
AED100 billion (US$27.7 billion) hospitality project, with the longest boulevard 
of hotels in the world, stretching 10 km. It will feature 31 hotels with a total 
of 29,200 rooms, including the largest hotel in the world with
6,500 rooms. </font></p>
