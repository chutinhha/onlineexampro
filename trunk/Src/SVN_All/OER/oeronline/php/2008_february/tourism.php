<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Changing face of global travel and tourism</font></b><font face="Verdana" size="1"><br>
<b>Jean Claude Baumgarten, President of World Travel &amp; Tourism Council-UK, 
underlines the co-relation between economic growth and its direct impact on the 
travel and tourism industry, besides forecasting that the Chinese will emerge as 
the primary drivers of global tourism in the coming years<br>
</b><br>
<img border="0" src="tourism.jpg" width="175" height="186" align="left" hspace="8" vspace="6">Travel and tourism is probably one of the biggest industries in the world. We 
have to raise awareness about it and that is what the World Travel &amp; Tourism 
Council (WTTC) is aiming to achieve. We work with various governments to realise 
the full potential of the industry in creating sustainable wealth and jobs.<br>
<br>
When we talk of travel and tourism, we need to follow the ‘economy concept’ 
where we look into economic terms such as GDP, demand, investment, expenses, 
etc. There is a direct co-relation between economic growth and its impact on the 
travel and tourism industry. Investments in development cast a positive impact 
on the travel and tourism sector. Dubai is a fantastic example. If you look 
around at the numbers of cranes operating in the city, probably 70 per cent of 
them must be devoted to infrastructure projects related to travel and tourism. 
So, that is what we call the iceberg. There is more beneath than that can be 
seen by the eye. <br>
<br>
<b>2008 headwinds</b><br>
As we look at global economic trends, we find that the headwinds for the world 
in 2008 include – credit crunch continues to be a worry on outlook; high oil 
prices may become a major constraint on growth; US housing is in trouble; weaker 
dollar and; China to record another year with two-digit growth.<br>
<br>
We believe that travel and tourism will continue to grow. Various factors are at 
work. The global per capita GDP is rising steadily. As a result of the hectic 
economic activity, the frequency of overseas trips per head is also growing. 
Travelling is growing faster than the GDP growth (see graph). The interesting 
scenario is that we need to travel faster to generate additional revenues to 
contribute to GDP growth.<br>
<br>
Also, despite increased downside risks, it remains likely that the slowdown in 
world GDP growth will be limited because of:</font></p>
<ul dir="ltr">
  <li dir="ltr">
  <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
  <font face="Verdana" size="1">rapidly expanding emerging economies </font></p>
  </li>
  <li dir="ltr">
  <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
  <font face="Verdana" size="1">these countries are recognising the development potential of the travel and 
tourism industry and investing accordingly</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
  <font face="Verdana" size="1">Rapid economic growth is boosting incomes and creating an entire new range of 
customers where international travel is now an option</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
  <font face="Verdana" size="1">the easing in monetary policy by central banks</font></p>
  </li>
  <li dir="ltr">
  <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
  <font face="Verdana" size="1">strong corporate profitability outside of the financial sector</font></p>
  </li>
</ul>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">We also need to have a look what is happening in China. Consider this: in 2020, 
there would be 100 million Chinese going overseas (see graph). Already, last 
year, there were 75 million, which is more than the Japanese. I have been in 
this business long enough and I’ve always found a Japanese tourist somewhere. 
But now I think there is a change of mind, as I can find two Chinese tourists 
everywhere.</font></p>
<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
<img border="0" src="tourism1.jpg" width="430" height="338"></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>Middle East destinations</b><br>The Middle East is a fantastic example of emergence of additional destinations. 
With businesses booming in the region and the market’s increasing integration 
with the world’s economy, more travellers are coming to destinations here than 
ever before. With projects running into more than US$1 trillion in various 
stages of planning and development, the Middle East is the region to watch out 
for. It is a classic example of how strong alternate destinations are emerging 
in the global travel and tourism market. <br><br>Business travellers exist the world over and airlines are enjoying this. There 
is a major co-relation between business travel and tourism. Travelling 
businessmen generally revisit destinations, mainly as part of their business 
activity and often as tourists, along with their their family members. <br><br>In 2007, the average percentage of travel and tourism sector’s share in the 
global economy was around 10 per cent. The US and Europe are still the biggest 
travel and tourism economies in the world. However, they are not growing as fast 
as other countries, for example Central Eastern Europe. Though the Middle East 
is not as big, nevertheless it is growing faster than the world average. Capital 
investment is increasing in the Middle East and tourism here is growing faster 
than what we expected it to grow.<br><br>By 2017, China will become the second biggest travel and tourism economy in size 
in the world. India will be in the seventh place. The US will still be the 
biggest travel and tourism economy in the world in 10 years. Places like Dubai 
have a lot of potential.<br><br>For UAE, the expectations in 2007 surpassed results from 2006 in all areas of 
tourism activities. In fact, the UAE experienced major increase in government 
expenditure, capital investment and visitor exports. Forecasts for the coming 
years will be more stable. In 2007, Qatar exceeded all 2006 results in most 
tourism activities, with the greatest increase being in business travel and 
capital investment. Bahrain also saw a marked increased in capital investment. 
All other tourism activities for Bahrain surpassed all growth expectations of 
2006. Oman looks to be stable and is likely to grow in the next 10 years.<br>
<br>As far as infrastructure is concerned, specially if you look at the UK, it is 
not up to the mark with regard to travel and tourism. There was a seminar held 
in London recently, where the discussion was about airport infrastructure and 
the subject of runways came up. It was then mentioned that no new runway had 
been built there after the Second World War. From this, you can understand the 
mission of travel and tourism. We raise awareness with governments about the 
importance of travel and tourism. The infrastructure in the Dubai airport is 
fantastic. As far as roads are concerned, if you go to India, you will see that 
the country is suffering from an acute shortage of proper roads. Travel and 
tourism will continue to grow in the emerging markets because there is a 
movement, which is strong enough to overcome the slowdown. </font></p>
