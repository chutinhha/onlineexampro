					<font face="Verdana" size="4" color="#C00000">Starcare 
					Health Systems comes to Oman</font></b></p>
					<p align="justify" style="margin: 3px 10px"><b>
					<font face="Verdana" size="2"><br>
					UK-based Startcare Health Systems is set to open hospitals 
					and clinics providing specialised medical care in Oman. A 
					Hari Kumar reports</font></b></p>
					<p align="justify" style="margin: 3px 10px"><b>
					<font face="Verdana" size="2"><br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/sep_passingby1.jpg" hspace="9" vspace="5" style="border: 0px solid #C0C0C0; " align="left"></font></b></p>
					<p align="justify" style="margin: 3px 10px">
					<font face="Verdana" size="1">Starcare Health Systems, a 
					United Kingdom-based health care consultancy company, which 
					also runs a chain of medical centres in UAE, is entering the 
					healthcare sector in Oman in a big way. <br>
					<br>
					Sadik Kodakat, chairman of Starcare Health Systems, UK, told 
					OER that initially the company will open a 50-bed hospital 
					at Seeb. The hospital will have facilities for laparoscopic 
					surgeries and advanced treatments for various complications 
					including orthopaedic and spinal problems. The initial 
					investment by the group in the healthcare sector is 
					estimated to be RO8mn. <br>
					<br>
					“Starcare plans to open another 150-bed hospital, around 10 
					polyclinics and a chain of 15 pharmacies in different parts 
					of the Sultanate in two-to-four years, Kodakat informs. The 
					company plans to enter the healthcare sector in Saudi Arabia 
					in future,” he adds.<br>
					<br>
					Kodakat says the aim of Starcare is to bring US and European 
					standards in the healthcare sector in Oman. <br>
					</font><b><font face="Verdana" size="2"><br>
					Need for new hospitals</font></b><font face="Verdana" size="1"><br>
					<br>
					Answering questions on the relevance of new private 
					hospitals in the healthcare sector in Oman, Kodakat says as 
					per WHO studies, there is a mismatch between demand for 
					healthcare and availability of facilities in the Sultanate.<br>
					<br>
&nbsp;Starcare will bring the next level of skills to the healthcare sector in 
					Oman, Kodakat says. Our effort is to raise the quality of 
					medical care available in the Sultanate, he says. On the 
					healthcare sector in Oman, Kodakat says, the ministry of 
					health had been doing a wonderful job overall and there was 
					no need for private hospitals in the past. Presently, as the 
					cost of medical care is escalating all over the world 
					governments are seeking private participation in the 
					development of healthcare sector. The situation in Oman is 
					also not different. Though there is participation of the 
					private sector in the healthcare sector in the Sultanate, it 
					has not developed on a par with other countries.<br>
					<br>
&nbsp;Our feasibility study has proved that there is need for high quality 
					healthcare in the Sultanate, he adds.<br>
					<br>
					“The Sultanate has several private players in the healthcare 
					sector but few internationally accredited hospitals,” says 
					Kodakat. Many hospitals in the private sector here focus on 
					volumes rather than quality. It is high time that the next 
					generation health care was brought here, he says. At the 
					same time, one has to be cost conscious in Oman market. 
					“Everyone is not affluent. Investors need to tailor their 
					strategies to ensure affordability with high quality.”<br>
					<br>
					Commenting on the trend of Omanis going outside the country 
					to seek specialised treatment, he says the most important 
					thing is to create confidence among patients about the 
					facilities in the country and to ensure access to treatment 
					without delay. “Our plan is to build a chain of satellite 
					centres to bring medical care close to one’s home.”<br>
					<br>
					</font><b><font face="Verdana" size="2">Alliances</font></b><font face="Verdana" size="1"><br>
					<br>
					On the possibility of strategic alliances, Kodakat says the 
					group has several years of experience in the healthcare 
					sector and is not going for any tie-ups with other hospital 
					groups here or outside the country. Hinting at the company’s 
					strategy in Oman, he said they are open to alliances with 
					insurance companies and government departments. “We have 
					already conducted discussions with different ministries in 
					this respect,” he said.<br>
					<br>
					The medical faculty, Kodakat says will include experts from 
					the region, other Asian countries and the West. The hospital 
					staff would be fully integrated to the system through 
					special training. The advanced operation theatres would be 
					built conforming to the guidelines of American Institute of 
					Architecture, he added.<br>
