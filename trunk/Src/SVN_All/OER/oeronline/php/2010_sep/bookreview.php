                    <i><b><font face="Verdana" size="1" color="#9A9A9A">A review 
					by Ganesh Sundararaman</font></b></i></p>
				<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                    <font face="Verdana" size="1">Vince Cable’s The Storm: The 
					world economic crisis and what it means raises a pertinent 
					question: “Supposing that monetary and fiscal policy fails: 
					what then?” This has already been answered by the 
					authorities in the same terms as Cable suggests. They have 
					resorted to “printing money”, the policy we now know by the 
					unlovely term “quantitative easing”. The question that looms 
					now is what happens if that, too, fails. To that there seems 
					no answer, not even from Vince.<br>
					Still, Cable’s little book is well worth reading. It is 
					probably the best book you can read to understand what on 
					earth is going on out there, and does the sort of job that 
					those blood-red Penguin Specials did decades ago. For the 
					intelligent layman, it chronicles complex events as it 
					explains them, pulling in just the right amount of wit, 
					history, anecdote and theory along the way.<br>
					<br>
					<br>
					</font><b><font face="Verdana" size="2">Ringside view<br>
					</font></b><font face="Verdana" size="1"><br>
					This former chief economist at Shell probably devotes too 
					much attention to the oil market and not enough to the 
					coming water crisis (the only serious blind spot in his 
					global vision). Thus Hayek, Malthus, Keynes and Marx are 
					interwoven with Northern Rock, Fannie Mae, Freddie Mac, the 
					South Sea bubble, Opec, the Doha trade talks, Mervyn King 
					and the rise of China and India to economic power, or the 
					“awkward newcomers” as The Storm characterises them. Cable’s 
					gift is for the succinct causal explanation, such as this on 
					how China’s emergence and export drive lie at the root of 
					the crisis: “While cheap manufactures created the conditions 
					for lower inflation and low short-term interest rates, the 
					vast accumulation of – mainly – Chinese foreign exchange 
					earnings manifested itself in the purchase of US government 
					bonds, keeping down long-term interest rates.<br>
					<br>
					Nicely put, and Cable rightly wonders aloud how it came to 
					be that the Chinese, where a billion still live on a dollar 
					a day, came to be lending the West money to buy Gucci and 
					BMWs and generally have a huge party. Uncomfortably for us, 
					Cable shows how “the British economic miracle of recent 
					years has been exposed as structurally unsound”. <br>
					It is difficult to argue with Cable’s conclusions: that the 
					idea that property ownership as a one-way ticket to 
					prosperity and a pension has been “a lie”; that foreign 
					ownership of UK economic assets such as the car industry and 
					the utilities does matter; and that neglect of manufacturing 
					and skills have left us in an economy where “only Polish 
					immigrants know how to repair leaking pipes and lay a 
					brick”.<br>
					<br>
					Cable brings a breadth of view and lightness of touch to 
					otherwise impenetrable subjects. The author is too modest to 
					point out that the economic storm engulfing us is a 
					“financial super storm of great destructive power”.<br>
