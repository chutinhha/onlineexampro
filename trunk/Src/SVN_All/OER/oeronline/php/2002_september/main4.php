                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Blazing a trail</font><p><font face="Verdana" size="1"><b>With a stunning 294 horsepower engine and matching off-road handling to boot, the new Chevrolet Trailblazer claims to be the most powerful 4WD in its class. Nayl D'Souza gives his impressions</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_september/50-car.jpg" align="left" hspace="5" vspace="5" width="225" height="136">The Chevrolet Trailblazer (2002) catches the eye as a rugged and comfortable sports utility vehicle (SUV). Connoisseurs of 4X4 will certainly appreciate the significant redesign work done by the General Motors team on the earlier incarnations of the vehicle. What has emerged is a charming and handsome performer off road and around sharp corners.&nbsp;<br>
                    <br>
                    <b>Appearance&nbsp;</b><br>
                    Brimming with confidence, the vehicle certainly excels in the looks department. It features body-on-frame construction with the auto industry's first hydroformed side rails to improve rigidity and sturdiness, according to Chevrolet.&nbsp;<br>
                    The Chevy's traditional bowtie monogram in the centre of the horizontal grill bar greets you at the front. At the rear, there's a convenient and practical step in the centre of the bumper, which makes it easy to reach the running boards on the roof rack. It also aids boot access. The twin round tail-lights over the reversing light at each corner are too understated. I would have liked to see something bold at the back that was as confident as the Trailblazer's graceful pillars and side window lines or as stylish as the headlamps split by the grill's cross-beam.<br>
                    <br>
                    <b>Undercarriage</b><br>
                    Sports styling goes more than skin deep in the Trailblazer. Hidden from normal view, the Chevy's chassis and underbelly feature beefy rack-and-pinion steering, muscular four-wheel vented discs with twin-piston callipers in front, independent front suspension with short/long control arms, solid rear axle using five-link suspension with Bilstein gas-charged shock absorbers and coil springs, and thick anti-roll bars front and rear. The spare tyre rests underneath the rear of the vehicle.<br>
                    The Trailblazer's track (the distance between the left and right tyres) is the widest in its class, 2.2 inches wider than of the Ford Explorer (2002 model). The vehicle has a tight turning circle of 36.4 feet, thanks to the suspension design and engine location. With the power steering, squeezing into a space at the City Centre car park and dodging stray shopping trolleys, was easy during a test drive, despite the fact that the SUV is 4.87 metres long.<br>
                    <br>
                    <b>Performance</b><br>
                    Advancements in electronics, metallurgy and manufacturing methods enable the Trailblazer's inline engine to soar. The new six-cylinder power-plant with variable valve timing is the key to the vehicle's civilised nature. The all-aluminium Vortec 4200 engine has excellent dynamic balance and even torque pulses produced from dual overhead cams and electronic throttle control. The result is the power of a V8 with the fuel economy of V6 configuration. The 4.2-litre engine produces a stunning 294 horsepower, 30 more than Ford Explorer's new 4.6-litre V8 engine. Yet, the powerplant is so quiet when the Trailblazer is stationary, that a feature called "intellistart" has been added to stop the starter from grinding if the key is turned on. The EPA rating is 15 mpg in the city and 21 on the highway. With all this in mind, I tended to let the throttle go a bit. Spurred by my persistent come-on-let's-have-some-more-revs attitude, most utilities would have belched, but not the Trailblazer.&nbsp;<br>
                    This SUV is designed to handle and ride like a car, with the option to scramble sure-footedly off the road, although here is another all-wheel-drive highway cruiser that in most instances will never see duty any rougher than on a gravel driveway leading to a pretty villa in Madinat Al Sultan Qaboos. This is a pity because the Trailblazer can cope fairly well with cross-country terrain. It has a transfer case that allows the driver to shift from 2 to 4WD drive on the fly. Of course, if it was pitted alongside a true off-roader like Toyota Landcruiser or Land Rover Discovery, it would be left a little bruised. But then, the Trailblazer was not built to be wrapped in cotton wool. Owners, who refrain from any strenuous activity that could scratch the fancy aluminium wheels or new paintwork, will not get to see the hugely impressive torque of the Vortec 4200 that gives this Chevy enough muscle to be the best tow-truck in its class.&nbsp;<br>
                    There is minimum rocking and vibration during travel, thanks largely to a rigid frame and careful engineering of all chassis and suspension components. Despite a relatively high centre of gravity and independent suspension fore and aft, the Trailblazer handles remarkably well with little body roll while cornering even at high speed on the wonderfully winding Qantab road. The Corniche stretch too was a steady and enjoyable drive even though I purposely landed the right-side tyres on nearly every rivet dividing the lanes!<br>
                    <br>
                    <b>Comfort and safety</b><br>
                    The Trailblazer has a luxurious amount of interior space. It is roomy for more than five occupants. Front seats are power controlled. They're comfortable but lack side reinforcement. In comparison, the GMC Envoy (2002), with a similar chassis and suspension, has better seats with side cushioning to keep you snug on long journeys.&nbsp;<br>
                    The instrument panel is neat and helpful. There is a manual dual-zone temperature control system for the occupants. Leg and headroom are adequate for back passengers. The rear-seat headrests flip conveniently down to provide better rearward visibility to the driver. For peace of mind, there are dual-level front airbags, seat-mounted side-impact airbags at front and three-point safety harnesses for all occupants.<br>
                    The car-maker has gone to town with little knick-knacks for added interior comfort. There is a travel-note recorder to store great ideas that suddenly spring to mind or perhaps to remind you about shopping. There are also a couple of overhead storage compartments for sunglasses, which is a wiz of an idea if, like with me, your shades too are always getting squashed in the glove compartment between audiocassettes and other bric-a-brac. However, the remote controlled power-mirror located on the driver's door was fiddly to operate.&nbsp;<br>
                    <br>
                    <b>The verdict</b><br>
                    The vehicle is available in three models internationally: LS, LT and LTZ. However in Oman, we get the LT (which is close to top-of-the-range as it comes with some standard options of the LTZ such as the travel note digital recorder, although automatic climate control and steering wheel controls for the audio centre and air conditioning are notably absent), and LTZ. The price tag could deter many from buying the Trailblazer. Even though this exciting vehicle has plenty to offer, for slightly more money, you could enjoy the GMC Envoy, Trailblazer's sibling from the GM stable. Or spend a little less and perhaps choose a Ford SUV such as the Explorer.&nbsp;<br>
                    Playing bridesmaid to other brands isn't much fun. The Chevrolet Trailblazer could one day become a clear winner in the midsize SUV category if it combines its current urban car comfort and off-road and towing abilities with further sophistication in styling and sensible pricing. There's already news that a seven-seat Trailblazer with longer wheelbase and completely new chassis is in the works and could arrive next year. Maybe a third row of seats will suffice to propel the Trailblazer ahead of the pack.</font></p>
