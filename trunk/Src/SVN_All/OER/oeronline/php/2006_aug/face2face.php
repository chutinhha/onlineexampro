<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">‘PDO has learnt some hard lessons’</font></b><font face="Verdana" size="1"><b><br>
</b><br>
<b>It’s time to pep up production levels of the oil fields by grabbing the 
technology-based opportunities, John Malcolm, Managing Director of Petroleum 
Development Oman (PDO), tells Mehre Alam<br>
</b>
<br>
<b>
<img border="0" src="pirscope1.jpg" align="left" hspace="5" vspace="2" width="80" height="116"></b></font><b><font face="Verdana" style="font-size: 11pt">S</font></b><font face="Verdana" size="1"><b>o far PDO has developed over 120 oil/gas fields in Oman. But why this variance 
between projections and actual production figures?</b><br>
For a rather long stretch — a few decades in fact — PDO’s projections, on how 
much oil it could produce in the coming years, were pretty much on the mark. 
Between 1979 and 1999, our production growth rate averaged nearly 10 million 
barrels per year! But there was a period — a few years — when PDO fell somewhat 
short of its production targets. Perhaps we were guilty of over-exuberance, 
hoping our oil production would continue to grow as it had previously, being so 
good at introducing the right developmental strategies at the right time.<br>
<br>
Moreover, at the start of the millennium, the natural production rate decline of 
our major oil fields eventually caught up with us. To make matters worse, our 
business had fundamentally changed. Our new fields were smaller in size. The new 
wells were delivering less oil. And our costs were going up. The fact was that 
our operating model — the way we were carrying out our business — was not 
sustainable. However, over the past three years too, we have managed to deliver 
what we promised to our shareholders.<br>
<br>
Today, we at PDO have to seize the opportunity that technology offers to counter 
the natural decline of production levels. Enhanced Oil Recovery (EOR) — 
application of heat, chemicals or gas solvents to alter the way oil or injected 
water flows in a reservoir — holds the greatest benefit potential amongst the 
panoply of technologies. We have had a sweeping change of programme following a 
comprehensive Operating Model Review that taught us some hard lessons. Now it is 
up to us to apply these lessons, and change the way we do our business over the 
next several years.<br>
<b>
<br>
PDO has met its oil production targets for the third consecutive year. But has 
the picture really been that rosy, if we look at average production and actual 
production?</b><br>
If you mean we are reaching our production targets now because the targets 
themselves have been set progressively lower — and therefore, by implication, 
easier — nothing can be farther from the truth. The challenges posed by the 
business targets, which our shareholders have recently set for the company, 
should not be underestimated. They are exceedingly tough.<br>
<b>
<br>
Perhaps the production targets for this year could have been set higher, and 
perhaps we could have met them. How? </b><br>
By simply drilling more wells. But next 
year? And the year thereafter? We do no favour to Oman by going all out one year 
and get a feast of oil, only to be followed by a year of famine. That’s a cycle 
which we have put behind us. Over the last three years, our performance shows we 
have started getting it right as far as managing our resources goes — and that’s 
how we intend to continue.<br>
<br>
Our Board of Directors has wisely decided to intentionally limit short-term oil 
production targets to between 580,000 and 600,000 barrels per day — even with 
the price of oil so high. This benefits the long term sustainability of the 
Company’s oil production.<br>
<b>
<br>
With production levels on a downslide, how have you been meeting production 
targets?</b><br>
It has been extremely challenging to meet the recent production targets, given 
the extremely tight cost constraints that have forced us to extract savings — 
US$2 billion over the five-year (2002-2006) period — from virtually every aspect 
of our operations. That too, when global economy is inflationary.<br>
<br>
Yes, meeting our targets for the next few years — till our major EOR projects 
are up and running — will be even more difficult.<br>
<br>
<b>How much has the EOR technique worked for the PDO?</b><br>
So far, we have produced very little oil from the EOR projects. That’s because 
we embarked on full-field projects fairly recently. The engineering, procurement 
and construction (EPC) contract for the Harweel EOR project was awarded only 
last year. The EPC contract for the Qarn Alam EOR project is expected to be 
awarded later this year. We certainly have high expectations from these 
projects. By 2010, we expect about 20 per cent production coming from these two 
EOR projects.<br>
<br>
<b>On a different tack, has PDO benefited from transportation related revenues 
from other players?</b><br>
If you are referring to the fees from other operators in Oman for transporting 
their crude oil to the coast through PDO pipeline network, then yes, there has 
been an increase in the total of what we call “through-put fees”, because more 
oil from other companies is flowing through PDO pipelines. But this increase in 
recoveries doesn’t amount to much, in comparison to our costs. It’s between 2 
and 3 per cent of our operational expenditure.<br>
<br>
<b>PDO has been providing an uninterrupted supply of natural gas to the 
government gas network for 27 years. What other plans do you have to increase 
the supply capacity to the nation’s gas liquefaction plant, other than the gas 
processing plant at Saih Nihayda and the 265-km ‘loop’ pipeline? </b><br>
With the commissioning of the Qalhat Liquefied Natural Gas Plant last year, we 
have pretty much reached a plateau — with a supply of around 30 million cubic 
metres per day to the nation’s gas liquefaction plants. But there’s still plenty 
of work to be done by our gas team in the area of project delivery, operational 
excellence and reservoir management to ensure that the supply remains 
uninterrupted. After all, a gas sales agreement typically has a lifetime of 25 
years, and PDO has to ensure that existing agreements are honoured.<br>
<br>
<b>Is there a move to rev up the gas part of PDO’s operations in the near 
future?</b><br>
First and foremost comes the development of the Kauther field, and the 
construction of a processing facility there. We also have plans to hook up the 
smaller gas fields to our processing facilities, and to install compressors at 
Saih Rawl and Barik fields. Our exploration department too has stepped up its 
drive to find new gas fields. Of course, there’s plenty of work to be done just 
to keep the existing gas fields producing efficiently.<br>
<br>
<b>PDO has now stopped injecting oil-tainted production water into the shallow 
aquifers at Marmul, Nimr and Rima. But isn’t it a case of responding to the 
environmental concerns a bit late?</b><br>
PDO has always been concerned about the disposal of its production water because 
it’s one of the major constraints of our oil production. On an average, for 
every barrel of oil, there’s five barrels of production water. All that water 
has to be got rid of one way or another. PDO works closely with all the relevant 
authorities in Oman to ensure that production water is disposed of in the most 
environmentally sound and practicable way. <br>
In fact, we have been disposing of this water with the full permission of the 
Ministry of Regional Municipalities, Environment and Water Resources. 
Nevertheless, we do acknowledge that disposal of production water in deep 
underground formations is preferable from an environmental point of view, which 
is why we have phased out shallow water disposal. <br>
<br>
We have also been experimenting, for several years now, with other novel means 
of dealing with our produced water — like irrigating the reed fields which 
actually removes the oil from water as they grow, or distilling it into potable 
water with the help of high-tech membranes and sunshine. <br>
<br>
<b>PDO has an excellent track record of producing some of the finest Omani 
administrators/managers. One prime example is the Minister of Oil &amp; Gas, H.E. 
Mohammed bin Hamed al Rumhy, who is an ex-PDO employee. Comment. <br>
</b><br>
It makes me feel proud! Ever since PDO started operation in 1967, we have been 
taking young, bright Omanis and giving them university education, managerial 
training and on-the-job experiences — with the hope that they will contribute to 
the development of their country through PDO. And those who leave PDO — and mind 
you, that’s just 2 per cent per year — for one reason or the other, continue to 
exercise the skills acquired at PDO in their other endeavours, ultimately 
benefiting the nation. This positive impact is often overlooked.<br>
<br>
<b>What is the percentage of Omanisation in PDO now?</b><br>
Slightly over 80 per cent, which is quite high for the industry. But I prefer to 
look at our Omanisation record in absolute terms. The employment graph has 
steadily going upwards — a record 3,784 in 2005 from 3,362 Omanis in 2002. 
Periodically, Deputy Managing Director Abdulla al Lamki and I have reviewed the 
career development plans of the top 150 odd Omanis in the Company. Result: 77 
promoted to senior positions in 2004, and another 86 in 2005. Indeed, the impact 
can be felt even at the topmost levels of the Company — half of PDO’s directors 
were Omanis at the start of 2006.</font></p>

