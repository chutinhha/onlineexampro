<b><font face="Verdana" size="4">
Joining Team Pajero</font><font face="Verdana" size="5"><br>
</font>
</b><font face="Verdana" size="1"><br>
<b>The Pajero Limited Edition has been upped in styling. But it does not make 
any compromises where performance is concerned, says Anne Kurian<br>
</b><br>
<br>
<img border="0" src="car_talk_1.jpg" align="right" hspace="5" vspace="2">The adrenaline rush is inevitable behind the wheel of a Mitsubishi Pajero. After 
all, Team Pajero has been winning the Dakar Rally for the last six years. And if 
that’s not enough, the 4WD has been exalted for its commendable off-road 
capabilities. Racing and off-roading. Now these two features were enough to get 
us excited about test-driving the Pajero Limited Edition.<br>
<br>
The Pajero has always been a favourite of off-road enthusiasts and rally 
drivers. The 4WD has metamorphosed over the years into a powerful vehicle with 
impressive agility on road, and as a reliable off-roader. The Pajero Limited 
Edition (LE) is no exception to this and has also been upped in styling 
considerably. The Limited Edition, both three-door and five-door models, are 
undoubtedly all-round performers — as we discovered both in the city and on 
rougher grounds.<br>
<br>
Equipped with a 3.8 litre V6, which incidentally happens to be Pajero’s most 
powerful engine so far, the 4WD seems to be able to take on most terrains 
smoothly. <br>
<br>
The powerful engine in the Pajero is complemented by the intelligent Mitsubishi 
Active Stability &amp; Traction Control system. The M-ASTC system, as we discovered, 
boosts the performance of the vehicle, yet allows you to have complete control 
of the Pajero. No wonder, this is one of the preferred vehicles for the Dakar 
rally.<br>
<br>
<b>The Outside Story</b><br>
At first glance, the Pajero LE may not seem to have been incorporated with too 
many changes. You would be forgiven for not noticing right away the enhancements 
— the size and shape of the 4WD is still the same as of the other models. Having 
said that, what does catch your eye first is the gold Mitsubishi emblem, which 
is otherwise silver in color. <br>
<br>
Get a bit closer and you notice the other add-ons in the LE version. This 
includes the side-step guard bars, front underguard and a very stylish front 
grille guard. The three-way adjustable discharge headlamps are undoubtedly among 
the most powerful in this category of 4WDs.<br>
<br>
The other cosmetic changes implemented in the Pajero LE include chrome-plated 
door handles and door mirrors (the latter caught the fancy of our photographer).
<br>
<br>
The LE’s also fitted with 17-inch polished aluminum wheels. This not only adds 
to the visual impact, but also provides better handling capability and more 
stability at the turns. <br>
<br>
<br>
<b>The Inside Story</b><br>
The LE comes en suite with quite a few luxury car features. Right from the plush 
leather seats, to the wood and leather accents in the cockpit and the A/C vents 
for the backseat passengers. Once inside the LE, you just cannot ignore the 
detailing, be it the ergonomically designed control panel or the backlit metres. 
The leather and wood finish on the steering wheel is a feature that’s exclusive 
to the LE — and it does enhance the driving experience.<br>
<br>
The illuminated RV metre on the centre of the panel is a welcome relief from all 
the small digital clocks in automobiles. The RV meter has a large time display, 
compass heading and also provides external and internal temperature readings.<br>
<br>
Leg and storage space inside the LE is impressive — comfortably seating five 
passengers including the driver in the two rows. And in case there’s more for 
company, then the third row of seats can be pulled up from the underfloor 
compartment for two more passengers. <br>
<br>
We liked the powerful six-speaker music system in the LE, especially the six-CD 
player. Though music may be the last thing on your mind while roughing it out in 
the rocky terrains, the six-CD player will be your perfect companion when 
cruising on the highway or heading out to Dubai. <br>
<b><br>
On ’n off the road </b><br>
The Pajero was made famous through the Dakar Rally. The rally takes place on 
some of the most difficult terrain where the drivers have to navigate through 
everything but paved roads. Though Oman many not have the Dakar Rally circuit, 
the country does have its share of rough terrain that can get an adventure 
seeker’s pulse racing. And once behind the wheel of the LE, the urge to step 
into the shoes of the Dakar rally drivers is inescapable.<br>
<br>
Without overdoing it for lack of experience, we decided to try out the LE on the 
rocky grounds in PDO Heights. We felt the first instance of the LE’s strength as 
soon as the 4WD gear was put into motion. Whizzing past the traffic on the Qurum 
highway, we had to make sure not to overspeed.<br>
<br>
The first area we tested out the LE was near the cemetery on PDO grounds. Off 
road, there are some really rocky land strewn with huge stones and blocks of 
hard mud in the area. Shifting the gear to 4WDH, designed to take on such 
terrains, we got the LE to speed over rocks and stones. And did the LE deliver! 
The terrain was a cakewalk for the LE as it ploughed on over rocks and cement 
chunks.<br>
<br>
Keyed up by the remarkable performance of the LE, we decided to try a bit of 
uphill driving in the same area. A few metres away we found the perfect 
location, though from ground it seemed to a bit too steep to overcome. But the 
excitement was too much to quell and we took on the climb in the LE. Here again, 
the LE went full steam ahead undeterred by the large and small boulders that 
came under its wheels. Not only did our photographer get some great shots once 
we were uphill, the LE also exhibited its steadfastness.<br>
<br>
The thrill of the LE experience was heady and we had to put the LE 3.8 L V6 to 
test once more — and this time, it was on the roads in and around Qantab. This 
is one of our favourite stretches. Step on the gas (petrol in this part of the 
world) and you are almost flying in the LE. You come down to terra firma only 
when you reach Barr Al Jissah. The LE proved to us in the one and half days we 
had it why the 4WD is such a hot favourite among the adventure seekers!<br>
<b><br>
Safe and sound</b><br>
The Pajero LE comes with all the works where safety is concerned. Front and side 
airbags, anti-brake system with electronic brake-force distribution for safe 
quick braking, and a resin guarded fuel tank that protects against leakage in 
case of an accident are some of the notable safety features in the LE. The 4WD 
also comes with an advanced engine immobiliser so that no hot wiring takes 
place!<br>
<br>
Mitsubishi, keeping in view the safety of everyone on board, has designed a 
monocoque body — rigid with a built in-frame that creates a shield around all 
the passengers. <br>
<br>
The Pajero Limited Edition undoubtedly is one of the best on and off road 
performers in its category. Dakar rally, PDO Heights or wadi-bashing, the Pajero 
is one vehicle you can rely on!<br>
<br>
<i>(Road test courtesy Zubair Automotive LLC, the dealer of Mitsubishi vehicles 
in the Sultanate of Oman.)</i></font></p>

