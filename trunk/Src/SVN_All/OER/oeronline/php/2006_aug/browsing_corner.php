            <p align="justify" style="margin-left: 5px; margin-right: 10px; margin-top:5; margin-bottom:5"><b><font face="Verdana" size="4" color="#FF0000">
            <br>
            </font><font face="Verdana" size="5">
            Managers &amp; Myths</font></b><font face="Verdana" size="1"><b><br>
            The Art of Tough Love: An Empirical Study</b><br>
            <br>
            <img border="0" src="brosing_1.jpg" align="right" hspace="5" vspace="2" width="150" height="203"></font><font face="Verdana" style="font-size: 11pt"><b>I</b></font><font face="Verdana" size="1"> must admit that even for a bookworm like me, this international 
            bestseller sat on my desk for two months. Conservative by nature, 
            breaking rules simply didn’t interest me. But then, curiosity took 
            the better of me — and reporting to the editor was inevitable! The 
            book, mainly, is of interest to Human Resource professionals, middle 
            level managers and frontline supervisors in every field. In fact, it 
            should be on their ‘must read’ list, as it is written with the help 
            of empirical evidence researched by Gallup Organisation, which has 
            conducted in-depth interviews with over 80,000 managers.<br>
            <br>
            The findings are startling. They reveal what great managers of the 
            world — who have very little in common — know. The revolutionary 
            insights shared by all great managers who differ in age, sex and 
            race, and who employ vastly different style and focus on realising 
            different goals. Yet, they all seem to have one common trait. They 
            don’t hesitate to break virtually every rule held sacred by 
            conventional wisdom. Over the last 25 years, Gallup came up with 
            hundreds of different questions on every conceivable aspect of the 
            workplace, and they shortlisted 12 questions which they used as a 
            yardstick. Again, I must admit that after reading those questions, I 
            was ready to junk the book. But patience prevailed. Seeing those 
            questions put to test — to measure productivity, profitability, 
            retention levels of employees and customer ratings of different 
            business units — I reluctantly read on... Some questions are 
            definitely more powerful than others, but managers are advised to 
            follow the pattern (or order) of the questions set out.<br>
            <br>
            <img border="0" src="br_cor.jpg" align="left" hspace="5" vspace="2" width="228" height="95">Now, what does the book really say! First, all ‘great managers’ need 
            to get “Strongly agree” responses to the first four questions, which 
            will then lead them to the selection of the right person, set 
            expectations and, in turn, motivate and develop the person. Most 
            importantly, it will help the manager to play the ‘catalyst’ role. 
            Second, if a company’s managers are unable to play this role well, 
            then no matter how sophisticated its systems or how inspirational 
            its leaders, the company will slowly start to disintegrate, say the 
            authors. Third, “healthy companies need strong bonds to develop 
            between each manager and each employee. If the manager hasn’t had a 
            say in selecting his people or team, and if he is not invested in 
            their current success and growth, then those bonds wither,” insist 
            Marcus Buckingham and Curt Coffman.<br>
            <br>
            The main strength of this book lies in its examination of how 
            managers should play this key role successfully in their respective 
            organisations — and thus achieve greatness. <b>This, precisely, is 
            where they go against all conventional wisdom that encourages the 
            following path:<br>
            </b><br>
            1. Select a person... based on his experience, intelligence and 
            determination.<br>
            2. Set expectations... by defining the right steps.<br>
            3. Motivate the person... by helping him identify and overcome his 
            weaknesses.<br>
            4. Develop the person... by helping him learn and get promoted.<br>
            Many readers would have experienced, at some stage or the other, 
            being stumped by the interviewee — who can range from the polite and 
            intelligent to the rude and crass. It is at this stage that managers 
            make or mar their business ventures. Hence, careful selection of the 
            right talent is required. Based on the empirical evidence, the book 
            examines the myths regarding spotting of talent.<br>
            <br>
            The second key, say the authors, is not just to define the right 
            steps but the right outcomes! Here they discuss the various 
            temptations managers can succumb to, and they also outline a few 
            rules. The central message is: It is best to trust and leave people 
            to do the jobs they are hired for, but with a pinch of caution, as 
            “unrestrained empowerment can be a value killer.”<br>
            <br>
            The third key urges managers to focus on strengths rather than 
            weaknesses. This makes it easier for them to help employees release 
            their true potential, and cast the right people for the jobs to be 
            done. There is a complete section on how to manage employees’ 
            weaknesses too.<br>
            <br>
            The fourth key warns managers against the temptation of unwarranted 
            promotions, termed by them as “the blind, breathless climb” — and, 
            instead, to use “broad banding.” Finally, it also teaches the “art 
            of tough love.” <br>
            <br>
            The concluding chapter is a practical guide for managers on the art 
            of interviewing, spotting talent, performance management and key 
            guidelines to be used by both employees and managers. Finally, one 
            has to appreciate the appendices that explain the basis of the 
            research conducted — which, after going through the book, is 
            remarkably exhaustive.<i>— Uma Devi Jadhav</i><br>
