            <b>...Each great manager was interviewed for about an hour and a half, 
            using open-ended questions. For example:</b><br>
            • “As a manager, which would you rather have: an independent 
            aggressive person who produced US$1.2 million in sales or a 
            congenial team player who produced about half as much? Please 
            explain your choice.”<br>
            • “You have an extremely productive employee who consistently fouls 
            up the paperwork. How would you work with this person to help 
            him/her to be more productive?”<br>
            • “You have two managers. One has the best talent for management you 
            have ever seen. The other is mediocre. There are two openings 
            available: the first is a high performing territory; the second is a 
            territory that is struggling. Neither territory has yet reached its 
            potential. Where would you recommend the excellent manager be 
            placed? Why?”<br>
            The answers to these, and hundreds of such exhaustive questions, 
            were tape-recorded, transcribed, read and re-read. Using the same 
            questions we then interviewed rather less successful colleagues. 
            These managers were neither failing nor excelling. They were 
            ‘average managers’. '<br>
            <br>
            Then we compared. We listened to 120,000 hours of tape. We combed 
            through 5 million pages of transcript. We searched for patterns. 
            What, if anything, did the best have in common? And what, if 
            anything, distinguished them from their less successful colleagues? 
            It turns out that great managers share less than you might think...</font></td>
