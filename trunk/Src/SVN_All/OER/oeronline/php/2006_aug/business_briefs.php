                        growth in flights and an increasing growth in 
                        transportation activity which includes passengers, 
                        flights, cargo and post services, according to HE 
                        Mohammed Sakhr Al-A’amri, Undersecretary of Civil 
                        Aviation Affairs at the Ministry of Transport and 
                        Communications. The average growth of passengers reached 
                        28 per cent during the first half of this year, as the 
                        number of passengers reached 2.30 million approx. 
                        compared to 1.80 million approx. during the same period 
                        last year. The number of departing flights increased by 
                        15 per cent during June 2006 to reach 1,940 flights, 
                        compared with 1,691 flights during the same month in 
                        2005. He attributed this significant growth in air 
                        transportation at the Seeb International Airport to the 
                        growth in Gulf Air flights, which were transferred from 
                        Abu Dhabi to Muscat. They grew by 46 per cent in June — 
                        from 521 flights in June last year to 761 flights last 
                        June. He also said some airlines, such as Oman Air, 
                        increased their flights since the beginning of this year 
                        to reach 450 last May, compared with 321 flights in the 
                        same month last year.<br>
