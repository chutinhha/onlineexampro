                        <b><font size="2"><br>
                        UMS unveils 11th Edition of Progress (2006-2007)</font><br>
                        <font size="1">To commemorate the 36th anniversary of 
                        the Sultanate’s Renaissance Day</font></b></font><b><font face="Verdana" size="1"><br>
                        <br>
                        </font><font face="Verdana" style="font-size: 11pt">
                        <img border="0" src="business_briefs_3.jpg" align="left" hspace="5" vspace="2" width="100" height="76">R</font></b><font face="Verdana" size="1">enaissance Day marks the beginning of the 
                        Sultanate’s journey into an era of social and economic 
                        development and political stability. United Media 
                        Services (UMS) celebrated this special day by presenting 
                        its commemorative publication Progress (Al Takkadum) 
                        2006-2007 for the 11th consecutive year. Unveiled by H.E. 
                        Ahmed Abdul Nabi Macki, Minister of National Economy, 
                        the latest edition of Progress acknowledges and applauds 
                        the futuristic vision of His Majesty Sultan Qaboos bin 
                        Said.<br>
                        <br>
                        <img border="0" src="business_briefs_4.jpg" align="right" hspace="5" vspace="2" width="200" height="163">“Progress is an important contribution towards our 
                        understanding of the way our nation has developed over 
                        the years. This book is a well-analyzed volume with rich 
                        content, capturing the progress of each of the eight 
                        regions of Oman”, said H.E. Ahmed Abdul Nabi Macki. He 
                        further commended UMS for the zeal and dedication that 
                        went into preparing and publishing Progress (Al Takkadum) 
                        2006-2007 and congratulated the entire team for their 
                        achievement.<br>
                        <br>
                        “On this Renaissance Day, we celebrate the developments 
                        that have flowed from His Majesty’s wise policies and 
                        that have transformed Oman into a modern and vibrant 
                        country. We rejoice in dedicating Progress (Al Takkadum) 
                        2006-2007 to showcase these developments”, said Sandeep 
                        Sehgal, General Manager of UMS. This year’s edition 
                        highlights the remarkable socio-economic and 
                        geo-political achievements that have transformed the 
                        Sultanate into a forward looking progressive nation. It 
                        takes a look at the vivid and tremendous metamorphosis 
                        at the grassroots level -strategically devised by the 
                        pragmatic vision of His Majesty Sultan Qaboos bin Said. 
                        The carefully put together publication goes beyond 
                        Muscat to capture and reflect the growth of the 
                        Sultanate at the regional level, examining on a 
                        macro-level, the positive changes that have taken place 
                        across the length and breadth of the Sultanate since 
                        1970.<br>
                        <br>
                        “It has always been a matter of pride and honour for us 
                        to bring out this publication in the best manner 
                        possible and value-adding to the presentation each 
                        year,” expressed Alpana Roy, Assistant General Manager, 
                        UMS. Thematically, this annual volume highlights the 
                        eight regions’ composite pace of progress in the spheres 
                        of education, health, social welfare, sports and 
                        culture, electricity and water, telecommunication and 
                        housing. It also mirrors the way the economy has grown 
                        in leaps and bounds as seen in the sectors of oil and 
                        gas, trade, agriculture and fishery and more recently 
                        tourism. Meticulously researched articles on social and 
                        economic developments, evocative reviews, and a 
                        spotlight on each region’s key feature — and its 
                        prospects bring to light the past, present and future of 
                        each of the Sultanates eight regions. It is a snapshot 
                        account of each of the eight regions’ development which 
                        ultimately empowers the people — the most important 
                        resource wealth of a nation.</font></p>
