                        <p align="justify" style="margin:5 10; "><font face="Verdana" size="1">
                        <b>Renaissance acquires Norsk Offshore Catering<br>
                        </b>
                        <img border="0" src="business_briefs_1.jpg" align="right" hspace="5" vspace="2" width="150" height="142"></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">R</font><font face="Verdana" size="1">enaissance Services SAOG (Renaissance) has acquired 100 
                        per cent shareholding of Norsk Offshore Catering AS (NOC) 
                        from Maersk Contractors Norge AS (MCN). Renaissance 
                        Chairman Samir Fancy stated on behalf of the Board of 
                        Directors: “We are delighted with the strategic 
                        implications of this initiative. The company’s Contract 
                        Services Group has expanded its capabilities to include 
                        offshore catering; the acquisition strengthens 
                        Renaissance’s core focus in the oil &amp; gas sector; 
                        underlines our international ambitions; and, most 
                        importantly, provides a platform for synergy between the 
                        group’s services businesses and marine &amp; energy 
                        businesses in key markets including the Gulf and the 
                        Caspian.”<br>
                        <br>
                        In related moves, Renaissance has announced the 
                        appointment of Ingvar Varhaug as General Manager of NOC 
                        based in Norway, and Keith Laddiman as Business 
                        Development Manager for the offshore oil &amp; gas sector 
                        internationally, based in UK.<br>
                        <br>
                        A high level delegation from Renaissance had visited 
                        Norway recently for meetings with NOC employees and 
                        union representatives, and NOC clients before concluding 
                        and signing the deal with MCN. Ever since MCN decided to 
                        divest NOC as a non-core activity, there had been 
                        considerable interest and competition for the 
                        acquisition amongst major international contract 
                        caterers. Renaissance’s Commercial General Manager for 
                        Contract Services, Ananda Fernando, explained, “Maersk 
                        has taken great care to select a buyer which understands 
                        and satisfies the highest safety, quality and customer 
                        service standards expected in the offshore oil &amp; gas 
                        industry in the North Sea.” He added, “The commercial 
                        details of the deal are subject to a confidentiality 
                        requirement. However, the scale of the acquisition can 
                        be understood by considering the current annual revenue 
                        of NOC at over RO5 million.”<br>
