<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<font face="Verdana" size="4">
<img border="0" src="mangment_1.jpg" align="right" hspace="5" vspace="5" width="172" height="151"></font><br>
<font face="Verdana" size="4">
Child Labour:&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
</font>
<font face="Verdana" size="6">A Closer Look
</font>
<font face="Verdana" size="6"> <br>
</font>
</b><font face="Verdana" size="1"><b>MIt’s not so easy to clearly mark the line 
between ‘child labour’ and ‘entrepreneurial children’ — more so, when families 
are struggling to subsist, feels David Batstone<br>
</b><br>
<img border="0" src="mangment_2.jpg" align="left" hspace="5" vspace="2" width="94" height="123"></font><font face="Verdana" style="font-size: 11pt; font-weight: 700">I</font><font face="Verdana" size="1"> recently spent a week with several child 
entrepreneurs from Peru. In a country where 50 per cent of the population earns 
less than US$2 a day, children are typically forced to work at a very young age. 
The kids I got to know wish to move from being day-labourers to becoming 
business owners.<br>
<br>
Frank Sales, a 15-year old who helps run a micro-enterprise fund for his peers, 
told me, “We have the desire and the work effort; we just lack access to 
capital!” His group helps nearly 15,000 kids get a start on creating their own 
ventures. Over the past 18 months, it has experienced a 95 per cent repayment on 
loans.<br>
<br>
In its programme, children are required to pay one per cent interest on 
principal, and put into personal savings one per cent of their profits. The 
micro-credit agency provides workshops on writing business plans, operating a 
budget and charting cash flow, including putting into place a simple accounting 
system.<br>
<br>
<img border="0" src="mangment_3.jpg" align="left" hspace="5" vspace="2" width="100" height="152"><b>Innovative Solution</b><br>
We were so inspired by their efforts that we made a pledge to double the size of 
their loan fund. Earlier this year, we established a Children’s Aid Fund so that 
we could use a portion of our own revenues — and donations from friends who 
wanted to pitch in as well — to help children around the globe who are designing 
innovative solutions for their future. <br>
<br>
Our initial effort is to make a clear distinction between ‘child labour’ and 
‘entrepreneurial children’. Political policies that seem so universal in the 
United States and Europe turn relative in other global communities.<br>
<br>
The condemnation of child labour is a good example. Nearly all Western human 
rights groups monitor it, and fair trade activists lobby to make it a standard 
for compliance. The best ‘compacts’ for corporate behaviour in global markets — 
the SA 8000*, for instance — ban employment of children under 15 years of age.<br>
<br>
But we discovered that the primary life skills strategy for helping street kids 
in Lima, Peru, was to put the kids to work. We work closely with Generación, the 
largest independent (no government funding) project for homeless youth in Peru’s 
capital. Generación’s goal is to teach kids as young as 11 years old employable 
skills so that they can fend for themselves, and not be forced to resort to more 
destructive trades. <br>
<br>
Young girls who once served as child prostitutes, for example, now run a 
for-profit bakery. Other children are taught landscaping skills. Generación 
helps them land jobs at city parks, government facilities, or private 
residences. The children earn a ‘salary’ and former street walkers manage nearly 
400 approved and trained landscape workers on their employee list.<br>
<br>
<b>More Than Money</b><br>
Lucy Borja, executive director of Generación, argues that work does more than 
just put money in kids’ pockets — it gives them a discipline otherwise absent in 
their lives. Moving them from the streets directly into a school is untenable, 
she says. There are no breadwinners at home.<br>
<br>
However, its landscape company does require kids to go to school for 4 hours a 
day in order to be eligible for employment. In addition, Generación consciously 
integrates learning into the landscape work. Managers place rows of seedling 
plants, for instance, in a formation of a multiplication table to help the kids 
learn their math (and help them to see the value of education for a work life).<br>
<br>
I would be remiss, however, to suggest that the line between ‘child labour’ and 
‘entrepreneurial children’ can always be clearly marked. For instance, last year 
a woman who monitors global purchasing at a major clothes retailer shared her 
dilemma with me.<br>
<br>
Due to the past exploitation of child labor, retailers are very sensitive to 
practices of ‘sweat labour’. In response, she told me, producers in 
underdeveloped regions sometimes organise into collectives that mask the family 
labour taking place in individual homes. In such cases, the producer can 
legitimately say that no child labour takes place at their work facility.<br>
But the ‘outside contractors’ may, very well, be families that put their own 
kids to work. Such practices violate her notion of ‘child rights’; but does that 
mean she should shut down families who simply are struggling to subsist?<br>
<br>
I myself have four children — a girl and three boys. Over the summer, they enjoy 
a time of leisure before they return back to school fulltime in the fall. I do 
everything possible to ensure that they “get to be kids” during this stage of 
their lives.<br>
<br>
I realise that I hold that expectation as a privilege — and a provisional notion 
at an economic moment in history. Political progressives need to be careful not 
to turn their own privilege into a road block for those who are not so lucky.<br>
<br>
<i>*Developed and overseen by Social Accountability International (SAI), SA8000 
is a global social accountability standard for promoting human rights and decent 
working conditions for workers — based on international workplace norms of 
International Labour Organisation (ILO) conventions, the Universal Declaration 
of Human Rights and the UN Convention on the Rights of the Child.</i><br>
<br>
<i>David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States.</i></font></p>

