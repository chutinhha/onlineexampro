<p align="left" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="4">‘Oman is among the vanguard in wireless 
technology’</font></b></p>

<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>On his maiden visit to the 
Sultanate, John Gage, Chief Researcher and Vice President of the Science Office 
in Sun Microsystems Inc., talks about his impressions of the state of IT in Oman 
in an exclusive chat with Divya Kumar</b><br>
<br>
</font><b><font face="Verdana" size="4">
<img border="0" src="pirscope2.jpg" align="left" hspace="5" vspace="2" width="80" height="117"></font></b><font face="Verdana" size="1">“</font><font face="Verdana" size="1">We’re just a computer company,” John Gage is fond of saying of the company he 
helped found in 1982. This may safely be considered the understatement of the 
year, for the company he’s referring to is IT giant Sun Microsystems Inc. But to 
Gage, the company, and indeed he himself, are simply facilitators in the 
on-going human project of connecting people, schools, businesses, governments 
all over the world — and allowing them to communicate.<br>
<br>
For him, it is all about the network, and more importantly, what can occur when 
people are given the chance to talk to each other easily across vast distances. 
And this is precisely the reason for his visit to Oman: “Computer people will 
assert, if you allow human beings to communicate, there’s more business; if you 
allow students and parents and teachers to communicate, there’s more learning. 
Oman is in the process of testing that.”<br>
<br>
Being Chief Researcher and Vice President of the Science Office at Sun, Gage is 
responsible for the company’s alliances with technical-scientific organisations 
and research institutions across the world, for governmental relations in areas 
of scientific and technical policy, and for international public policy. He was 
recently in Muscat to meet senior government officials and lend his years of 
expertise and experience in creating knowledge networks to the Omani initiative 
of e-governance, and collaborative learning in the education sector.<br>
<br>
“Oman has very serious plans to link everything to the network,” he said. “I 
feel we’re partners in Oman in a very important project. OCS Infotech, led by 
C.E. Potnis which represents us in the Sultanate, is doing a fantastic job in 
contributing to the technology leap in Oman.”<br>
<br>
<b>Rural Connectivity and Education</b><br>
It was clear that Gage was impressed with the steps already taken by the Oman 
government in this direction, particularly with the deployment of CDMA (code 
division multiple access) networks in rural areas. “Oman is among the vanguard, 
among the leaders, in introducing some of the most advanced wireless 
technology,” he said, stressing on the importance of the push that has been made 
towards rural connectivity.<br>
<br>
“Ordinarily, people will take these CDMA high-speed digital networks and put 
them in the city where people will be happy to pay,” he pointed out. “About a 
million and a half of the Omani population lives in the rural areas and in very 
separate, isolated communities. The Ministry’s deployment of this CDMA 
technology through Omantel in the rural areas means not only that these people 
can talk to each other, but also go on the Internet to compare prices, check 
weather, all the things that affect us everyday. The ability to communicate with 
each other means that markets exist. That’s the long-range impact.” <br>
<br>
Gage was also very appreciative of the government’s dedication to get the 
educational institutions on the network, with 40 per cent of schools already 
connected to the Internet, and governmental plans underway to make that 100 per 
cent. Indeed, education as the foundation of building a knowledge-based economy 
was the focus of his discussions with Ministry officials about Oman’s use of 
computing and networking. He also addressed that the challenge is to make sure 
that communication costs are low enough to allow everyone to participate on the 
network.<br>
<br>
“The focus has to be on enabling children, and giving them the sense that they 
can also do meaningful exercises,” he said. This is clearly a subject that is 
close to Gage’s heart. In 1995, he created NetDay, a volunteer project to bring 
the resources of technology companies worldwide to schools and libraries to 
connect them to the Internet — through which over 500,000 volunteers have wired 
over 50,000 schools and libraries in the United States. He is on the board of 
NetDay and Schools Online, a non-profit organisation dedicated to “connecting 
the world one school at a time.” <br>
<br>
He shared with enthusiasm discussions underway to link all the technical 
institutes scattered all over the Sultanate on to the Internet, so that they may 
share a common curriculum and collaborate with each other. Such connectivity, he 
said, was the key to the development of universities, and diverse engineering 
faculties in the region which, in turn, could serve as a cradle for 
technological innovation: “Now that the network allows you to share information 
something could flower, something that might have taken 20 years in the past 
could happen much more rapidly.”<br>
<br>
In fact, Gage said, the next big idea such as Google (which was dreamt up by two 
graduate students in Stanford University) could just as easily come from here as 
anywhere else in the world. As he pointed out, the Sun Microsystems itself was 
born in Stanford University and University of California, Berkeley, and the 
corporation’s very foundation lies in the university world. <br>
<br>
<b>Cultural Bonding in the ‘Participation Age’ </b><br>
“We were founded on the idea of the technology to be shared, to be a base from 
which you can create something,” Gage said, expanding on Sun’s philosophy. “We 
put all the work we did openly on the network — allowing millions of people to 
add, improve, change etc.” Welcoming the ‘Participation Age’, he described it as 
“a reduction of the barriers to creative and intelligent people sharing and 
building upon each other’s experience, and creating something that can transform 
the way we see the world.” <br>
<br>
That style of open-source business — open to creativity pouring in from 
everywhere — has remained a part of Sun’s vision, except for a period in the 
late 1990s and early 2000s, according to Gage. “There was an enormous amount of 
dotcom money then, and we went astray for a bit, and. made our open-source 
policy less open,” Gage commented. “We’ve reversed that and it’s totally 
open-source now. Everything we do is open, anyone can take it, modify it, add to 
it, improve it, change it, extend it, create new products from it.”<br>
<br>
A case in point is the ‘OpenSparc’ project, through which the code and 
specifications of their breakthrough eco-friendly, power-efficient 8-core 
UltraSparc T1 processor was published earlier this year to spur innovation in 
processor design and architecture.<br>
<br>
“We’ve open-sourced the hardware, not just the software,” Gage explained. “What 
that means is that, China, attempting to become a fundamental force in world 
hardware design, is taking the open-source design of our Sparc chips and is 
beginning to implement them, which is fine with us. So we’re trying to change 
the dynamic of computing. And I think we’ve just begun. Many ideas of the 1960s 
about decreasing barriers of commerce, we’ve not yet realised.” <br>
<br>
Certainly business barriers are being decreased in Middle East, and in a sense, 
that is the ultimate purpose of Gage’s visit. Sun’s presence across the GCC 
states “has grown remarkably,” he said. They currently employ upwards of 200 
people in the region. As a 100 per cent partner-leveraged company, not only is 
Sun investing as a multinational in terms of getting people connected to the 
network, there is also a strong focus on transferring skills into the local 
markets through their partner-advantage programme.<br>
<br>
“We want to make our partners stronger,” Gage said. Indeed, that was the focus 
of the discussions during his visit to Oman — what could be done to accelerate 
the e-governance portal projects, and the education projects being planned in 
Oman. “The trivial answer is to make cheaper, more powerful, more 
energy-efficient computers,” Gage mused. “Well, we’re doing that.”<br>
<br>
The harder part is what Gage called a kind of “cultural bonding,” a meeting of 
ideas and sharing of problems, and applying of solutions that have worked 
elsewhere locally; in other words, a participation in the building process 
across cultural borders.<br>
<br>
To that end, Gage intends to visit Oman again — perhaps in November when the 
weather is kinder, as he said — to fully experience the beauty and culture of 
the country. Meanwhile, he remained upbeat about the potential for technical and 
business growth in the Gulf: “In the next six to eight months, we will see an 
explosion in the region,” he predicted.</font></p>

