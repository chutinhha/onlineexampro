<b>What the future holds</b><br>
In general, John Gage had a tremendously positive outlook about the future of 
the network. The Chief Researcher and Vice President of the Science Office in 
Sun Microsystems Inc.was absolutely clear on the fact that communication 
technology such as cell phones will become more and more common place. “There is 
an enormous upward growth of the desire of people to talk, to flirt, to do 
things that are human... So these things are going to become ubiquitous,” he 
said. He also predicted that technologies of location and identity would become 
equally widespread. “Objects will become identified; billions of things in the 
retail chain will become identified; vehicles, anything moving, will be 
identified; every drop of water wasted will be identified,” he said. This has 
the potential to improve everything from the efficiency of business logistics to 
something as basic as availability of clean water worldwide. “In all, the 
betterment of human life on the planet is moving in a positive direction,” he 
concluded.</font></td>
