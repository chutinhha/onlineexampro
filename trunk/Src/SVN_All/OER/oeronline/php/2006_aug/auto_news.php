                  <font face="Verdana" style="font-size: 16pt">Towell Auto Mall
<br>
revolutionises auto marketing</font></b><font face="Verdana" size="4"><b><br>
</b></font><font face="Verdana" size="1"><br>
<b>Towell Auto Centre’s smart strategy of reaching out to the customers by 
creating an ‘Auto Mall’ within a shopping mall has paid a rich dividend for the 
company<br>
</b><br>
</font><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1">lmost a year back, Towell Auto Centre introduced a new innovative customer 
touch point — auto mall within a mall — as part of an aggressive marketing 
strategy. “What was conceived purely as a marketing aid, has crystallised today 
as a mall within a mall, contributing a steady amount every month to the sales 
budget,” shares Paul J. Valiyaveetil, Manager, Retail Sales, Mazda, with Towell 
Auto Centre LLC, at the end of one year of operations of the auto mall at Markaz 
Al Bahja. There is a certain sense of achievement and consequent satisfaction in 
his voice as he says this, since the auto mall was aimed and planned with a view 
to giving a better display to cars, with a dedicated marketing team to service 
the display.<br>
<br>
<img border="0" src="auto_news_5.jpg" align="left" hspace="5" vspace="2" width="150" height="112">Another reason behind setting up the mall was to give the customer a choice 
across brands. A showroom would stock cars from one single manufacturer. On the 
contrary, Towell Auto Mall gives the customer a multi-brand presence by dint of 
the fact that it showcases the Mazda (a Japanese make) and SEAT (a VW brand), 
with distinct display areas and service staff.<br>
<br>
The customer has been at the cornerstone behind the thinking of setting up the 
auto mall. Women are playing an increasing role in the family’s decision to 
purchase cars. Plus more number of women are driving on the roads in Oman than 
ever before. “Rarely, however, would you find a lady in a standalone auto 
showroom, despite the fact that buying a new car or upgrading to a new model 
could essentially be her decision. With the auto mall within a mall, we have 
families coming together for shopping and simultaneously check out a new car 
model. This is an entirely new market segment that has opened up for us,” says 
Paul. He adds further, “In our CRM exercises, we have found that there is a very 
high correlation between the comfort of “auto shopping” within a mall and 
actually making a purchase decision.”<br>
<br>
In addition, the convenience of checking out a car late in the evenings with the 
whole family is a bonus that a standalone auto showroom cannot provide. Markaz 
is open till 10 in the night whereas auto showrooms generally shuts shop around 
8 pm. “The mall within a mall is more of a lifestyle statement which goes very 
well with the local habits of shopping till late in the night. Also, Markaz has 
a steady expatriate customer flow who have premium tastes and a comparatively 
higher affordability level. Thus, our mall caters for several customer 
segments,” adds Paul. <br>
<br>
“When the idea of the mall was chalked out, that it would result in direct sales 
was not foreseen,” says an elated Paul. Markaz has an average footfall of 1500 
to 2000 per day. Of these, around 40 to 50 people visit the auto mall. “As a 
result of what customers see at at Markaz, at the end of a month, we have, on an 
average 12 units sold from our four showrooms across Muscat, translating to 
roughly RO78,000,” says Paul.</font></p>

