            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <b><font face="Verdana" size="4">Omani Brands Leading The Way<br>
            </font></b>
            <font face="Verdana" size="1"><b>More and more Omani brands are 
            visible on the hypermarket shelves. Akshay Bhatnagar takes a look at 
            how Omani brands have carved out a niche despite stiff competition 
            from imported brands — and the role played by the government in this 
            transformation</b><br>
            <br>
            </font>
            <b><font face="Verdana" size="4">
            <img border="0" src="cover_1.jpg" align="right" hspace="5" vspace="5" width="198" height="198"></font></b><font face="Verdana" style="font-size: 11pt"><b>M</b></font><font face="Verdana" size="1">any 
            of us start our day by getting up from a Raha mattress, slip into a 
            dress washed with Bahar detergent, drink Mumtaz tea made with Zain 
            milk, eat Nabil biscuits and omelette made from Barka eggs in Minara 
            oil, drive to our office in a car running on an Antara battery, use 
            computers powered by wires from Oman Cables in the office cabin 
            having Sadolin paint on the walls and Al Shams tiles on the floors. 
            Just look around and you will see how much Omani brands have become 
            an integral part of our lives.<br>
            <br>
            Step into any hypermarket; chances are you are bound to see Omani 
            brands craving for your attention from the shelves of most of the 
            products. Those who are new to the Sultanate may not register it. 
            But for the local community as well as the expatriates who have been 
            here for quite some time, the transformation is fairly visible. Over 
            the years, a fairly large number of homegrown brands have come up, 
            and some even overshadow the foreign brands that were dominating the 
            scene earlier. Quite a few Omani brands have scaled up to the market 
            leadership status too. <br>
            <br>
            <b>Marching ahead</b><br>
            A few months ago the Ministry of Commerce &amp; Industry (MoCI) 
            appointed Advanced Business Consultants (ABC) to do an in-depth 
            study Omani Products – Consumer Perception to gauge the consumer 
            behaviour towards Omani products. ABC contacted a select number of 
            consumers in different regions of the Sultanate. After months of 
            field survey and data analysis, ABC submitted its report based on 
            the survey findings, and its recommendations, to the ministry. The 
            report’s summary is expected to be released to the media in 
            September — but OER managed to get an exclusive preview of the study 
            findings.<br>
            <br>
            The MoCI-ABC findings are definitely a matter of concern for the 
            international brands and a matter of pride for the Omanis. The study 
            indicates that Omani brands and products are firmly entrenched in 
            Oman, with significant displacement of the imported product space. 
            The propensity to buy Omani products is also high for most of the 
            categories.<br>
            <br>
            <b>Food &amp; Household Category</b><br>
            • As many as 17 of the 29 products (from the sample) in this 
            category have developed to become a dominant or ‘halo’ Omani brand — 
            there are four ‘champion’* and 13 ‘above average’ products. The 
            aggregate volume of Omani manufactured product domination in these 
            products exceeds 65 per cent in terms of market share.<br>
            <br>
            • At the bottom end of the hierarchy in this category, there are 
            seven products where imported brands still hold sway, but where 
            Omani brand awareness and favourableness of disposition indicate a 
            propensity to an increased market share in the future.<br>
            <br>
            • The ‘below average’ range in this category constitutes five 
            products where the degree of penetration by the Omani products has 
            influenced, to some extent, the market dynamics. The aggregate 
            volume of Omani manufactured products in this category is 
            approximately 36 per cent in terms of market share.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            <br>
            <img border="0" src="cover_2.jpg" align="left" hspace="5" vspace="2" width="80" height="100"><br>
            In the winning products section, it’s largely a case of one or two 
            winning Omani brands dominant in their respective categories, 
            followed by other lesser known Omani brands. While market share may 
            not be high for the lesser known brands, there has been a 
            significant improvement in awareness in almost all categories of 
            products.<br>
            <br>
            The MoCI-ABC report finds that each Omani product group has two main 
            types of successful brand creators:<br>
            • Brands from Omani manufacturers who are well entrenched in the 
            Omani market for very long periods.<br>
            • Brands from Omani manufacturers having a formal collaborative 
            membrane with an established global manufacturer.<br>
            <br>
            <b>Star Performers</b><br>
            The food sector has achieved a high degree of homogenisation, 
            closely followed by the consumer household products category, 
            indicates the MoCI-ABC study. The report identified a set of 
            products — clubbed as ‘Locomotive Sector’ — which have competitive 
            brands and are significant contributors to the local economy.<br>
            <br>
            The products in this category include mineral water, batteries, 
            dates, juices, milk, steel furniture, industrial cables, aerated 
            soft drinks, ceramic tiles, eggs, mattresses, poultry meat and 
            tomato ketchup.<br>
            <br>
            <img border="0" src="cover_3.jpg" align="left" hspace="5" vspace="2" width="78" height="99">Some 
            of us may take the report findings with a pinch of salt — but look 
            around in the market and you will agree that the trend (as the 
            report indicates) is becoming a reality.<br>
            • In the mineral water category, we have home grown brands such as 
            Salsabeel, El Jabal El Akhdar and Tanuf belonging to National 
            Mineral Water Co. SAOG, besides others, giving a hard run to MNC 
            brands like Aquafina.<br>
            • Sultan foodstuffs, part of W J Towell &amp; Co LLC’s Sultan Products 
            Division, are enjoying an increasing consumer loyalty in the region.<br>
            • Suroor, the chips brand from Mehdi Foods LLC, is a force to reckon 
            with even for global brands like Lay’s.<br>
            • Areej Vegetable Oils &amp; Derivatives SAOG’s Minara is the top 
            selling sunflower oil brand in the market.<br>
            • Moving to the biscuit category, National Biscuit Industries Ltd. 
            SAOG’s Nabil brand is way ahead of the competition including names 
            like Nutro, Tiffany and McVities.<br>
            • Some of the world’s favourite food service brands like KFC and 
            Pizza Hut in Oman prefer to use food products of AATCO LLC which has 
            a range of Omani brands.<br>
            • Amouage LLC’s range of premium perfumes under the brand name 
            Amouage is a rage in the upmarket lifestyle product category.<br>
            • Reem from Riyam Investment and Trading Establishment LLC is quite 
            popular in the personal care category especially eau de colognes, 
            roll-on deodorants and air fresheners.<br>
            • Detergent brand Bahar, part of National Detergent Co. SAOG, is a 
            classic example of how Omani brands can take on the world’s best if 
            they have the courage of conviction.<br>
            • Oman Cables is ruling the roost when it comes to wiring cables in 
            the Sultanate.<br>
            • Raha range of mattresses, pillows, bed linen, upholstered 
            furniture and other furnishing items are the preferred choice for a 
            large base of consumers.<br>
            • Al Shams brand of ceramic tiles, part of Al Anwar Ceramic Tiles 
            Co. SAOG, holds sway in a product category which is not known to be 
            brand led, and has competition coming from China, Italy, Spain and 
            other GCC countries.<br>
            • Reem Batteries &amp; Power Appliances Co. SAOC’s dry charged battery 
            brand, Antara, commands leadership in a market characterised by a 
            plethora of imported brands and local products riding on price 
            parity.<br>
            • Oman Foodstuff Factory LLC’s Al Mudhish range of foodstuff 
            including milk powder, potato chips and tomato paste etc. are 
            popular in Oman and abroad.<br>
            • In the paints segment, the wide acceptance of Sadolin Paints Oman 
            LLC’s range of paints has quelled the perception that only imported 
            paints are of good quality.**<br>
            <br>
            <img border="0" src="cover_5.jpg" align="left" hspace="5" vspace="2" width="110" height="112">The 
            list of Omani brands making their presence felt in the market is 
            quite exhaustive — with a number of small and medium size brands in 
            different stages of their lifecycle also fighting it out in the 
            market.<br>
            <b><br>
            </b><b>Winners despite odds</b><br>
            Most of the brands doing very well have one thing in common. They 
            all had to fight against all odds to prove that Omani brands could 
            succeed in their respective product categories. Some of the issues 
            stacked against them were:<br>
            <br>
            • <b>Raising finance for the entrepreneurs was a major issue in the 
            initial stages —</b> Recalling the early days, Hussain Bin Salman 
            Al-Lawati, Vice Chairman &amp; Managing Director of Oman Cables 
            Industry, says, “Way back in 1984 when there used to be very little 
            manufacturing activity taking place in the Sultanate, we decided 
            that we must get into manufacturing cables as we could sense a great 
            opportunity underlying there. But most of the institutions and 
            people we approached for financial support refused to invest in our 
            idea. With great difficulty — to the extent of even landing up with 
            heavy personal debt — we managed to translate our dream into 
            reality.”<br>
            <br>
            • <b>Consumers initially refusing to consider Omani brands as 
            quality products — </b>Munir M Khan, General Manager, Sadolin Paints 
            (Oman) Ltd. informs, “In the initial phase, Sadolin in Oman was 
            considered a local brand against the imported brands. Therefore, 
            Sadolin products were always seen to be inferior to the other 
            internationally manufactured products.”<br>
            <br>
            What about the behaviour of expatriate segment of the consumers 
            towards the Omani brands? Prem Maker, Executive Director, Areej 
            Vegetable Oils &amp; Derivatives SAOG replies, “The perception of any 
            brand, Omani or non-Omani, depends on the individual brand image and 
            personality. If we take the case of Minara, it is designed to appeal 
            to both Omanis and non-Omanis, and is favourably considered by both 
            groups of consumers.” <br>
            <br>
            Saibal Sen, Director &amp; General Manager, National Mineral Water Co. 
            SAOG, has a similar opinion: “Omani products are periodically tested 
            by the Ministry of Commerce &amp; Industry. Even the expatriate segment 
            has good faith in the quality of Omani brands.”<br>
            <br>
            • Convincing the consumers to pay a high price for an Omani product, 
            the way they do for an imported quality product — Syed Anwar Ahsan, 
            General Manager, Poly Products LLC, says, “Raha has always believed 
            in providing the best quality to its customers, due to which its 
            products are not always the cheapest. It hence took quite a bit of 
            effort in convincing customers that Raha products are good value for 
            money considering their usable life period.” <br>
            <br>
            <img border="0" src="cover_8.jpg" align="left" hspace="5" vspace="2" width="74" height="112">Being 
            the first movers, National Biscuit Industries Ltd faced challenge on 
            the marketing front. N S S Subramanian, Regional Marketing Manager, 
            explains: “Nabil was the first biscuit manufacturing company to 
            produce biscuits in the GCC. Though Nabil was the first Omani brand 
            in the market in the biscuit category, there was stiff competition 
            from most of the international brands.<br>
            <br>
            “In general, resistance to switch brands is a normal thing among 
            consumers — and in our case, overcoming the resistance and make them 
            switch to Nabil was really a challenging task. This was achieved 
            mainly through rigorous product sampling and ensuring excellent 
            availability of products, at all times across all parts of Oman, 
            with our own distribution network,” shares Subramanian.<br>
            <br>
            A clutter of international brands jostling for space in the market 
            makes the task difficult for a new brand, especially an Omani one, 
            to carve a niche for itself. Radha Mukherji, Executive Director, DDB 
            Oman, has managed brand communication for a number of Omani brands 
            in the market.<br>
            <br>
            Shedding light on this dilemma for Omani brands, she says: “Building 
            a brand is building a brand, and there is no unique set of rules for 
            building an Omani brand versus any other. There are just strong 
            brands and weak brands in any market, and this is as true for 
            America as for Oman.”<br>
            <br>
            <img border="0" src="cover_9.jpg" align="left" hspace="5" vspace="2" width="84" height="134">“The 
            difference between strong and weak brands comes about because of the 
            different levels of understanding of the potential power of the 
            brand, and investment made, in building and sustaining it. 
            Certainly, it is easier to invest more in brand building when the 
            market base brings bigger returns. So, in terms of sheer scale, 
            local brands in small markets will always be at a disadvantage, 
            compared with international brands,” Mukherji elaborates. <br>
            <br>
            “Thus, Omani brands will have to work harder and invest more, 
            relative to revenue, in order to hold up to international 
            competition even in the home market. I am not talking about just 
            investment in the image created through advertising but in every 
            aspect of business — from investment in technology, manufacturing 
            processes, quality control, staff training, to the final delivery of 
            the brand to the consumer in terms of packaging, advertising 
            promise, service and so forth. The principles of building and 
            sustaining brands are basically the same, the investment required, 
            relative to returns, can be steep in smaller markets, like Oman,” 
            concludes Mukherji.<br>
            <br>
            As she, has pointed out, the small size of the market makes the task 
            more difficult for an Omani brand. Perhaps that’s the reason why 
            most of the Omani brands have sizeable exports to make the 
            financials of the company more healthy. In some of the cases, almost 
            two-thirds of the revenue comes from the export market.<br>
            <br>
            <img border="0" src="cover_8.jpg" align="left" hspace="5" vspace="2" width="74" height="112">Some 
            of the companies sell their products under a different brand name 
            outside Oman, and some even sell their products to foreign companies 
            who sell under their own brand names. In a way, the acceptability of 
            Omani products in the international market indicates that Omani 
            companies are increasing focus on the quality front, as 
            international quality norms are quite stringent.<br>
            <b><br>
            Government’s support to Omani brands</b><br>
            National Products Promotion Committee has been set up under the 
            Ministry of Commerce &amp; Industry (MoCI) “to create awareness and 
            promote Omani products.” Manal Mohammad Al-Abdwani, Director General 
            of Planning &amp; Follow-up, MoCI, heads the committee (see her 
            interview on page 27). The committee is running a special ‘Omani 
            Products Promotion Campaign’ to meet its objective.<br>
            <br>
            Briefing about the activities and plans of the campaign, Khalid 
            Saeed Al-Shuaibi, Head of Executive Office for the campaign and 
            member of the committee, says, “In 2005, we launched a three-tier 
            product promotion plan — among government authorities, school 
            children and the general public.” The three-tier promotional plan 
            was:<br>
            <br>
            • An awareness exercise about the Omani products in the government 
            offices so that, while making a purchasing decision, the government 
            authorities could prefer Omani products.<br>
            • Various competitions based on Omani products organised among the 
            children. A proposal to include information about Omani products in 
            the school curriculum is also under consideration.<br>
            • For the general public, special exhibitions in hypermarkets and 
            supermarkets besides retail shops in gas stations organised on a 
            regular basis.<br>
            Khalid adds, “We have seen that with every special promotion, the 
            sale of Omani products increases by 25-30 per cent. In some cases, 
            it even goes up to 200 per cent.” Theatre shows and radio programmes 
            figure among some of the popular promotional activities.<br>
            <br>
            <b>
            <img border="0" src="cover_16.jpg" align="left" hspace="5" vspace="2" width="80" height="100">...But corporates want more</b><br>
            Most of the companies contacted by OER expressed satisfaction with 
            the government’s support. They appreciate the government’s efforts 
            to strengthen them to come out of tough situations. Munir M Khan of 
            Sadolin Paints acknowledges: “When the company started in 1977, 
            initial years were very tough. But the government’s support of 
            import duty on paints also helped in improving our economics at low 
            volumes.” <br>
            <br>
            But some of the corporates feel that the government can do a lot 
            more. Mohamed Ali Abdul Amir Sultan of Sultan Products Division, W J 
            Towell &amp; Co LLC, suggests, “There should be support on export 
            guarantees. Transportation facilities, ports infrastructure and 
            shipping line services could be enhanced for more and efficient 
            delivery of goods. Legal costs should be more supportive — whether 
            they are governmental or through local embassy. Cost of shipment 
            charges should be reasonable. Also, some more export incentives will 
            help Omani products achieve international stature.” <br>
            <br>
            S Gopalan of Antara batteries believes that more needs to be done, 
            especially in the area of protecting the consumer from the influx of 
            sub-standard products. He says, “It is no secret that many of the 
            brands available in Oman today are well below the minimum quality 
            levels specified under international standards. In fact, a number of 
            such brands are so poor in quality that their performance parameters 
            barely cross the halfway mark. The use of such substandard products 
            is likely to have a damaging impact on the consumer’s vehicle. 
            Therefore, the government should take a serious look and advise 
            suitable measures to curb the entry of such products.”<br>
            <br>
            With or without government support, the tougher lot has proven that 
            if one could produce a quality product and market it strategically, 
            then the tag of an Omani brand is not a bottleneck. But the 
            successful Omani brands need to be ready to face more competition. 
            The Sultanate’s greater integration in the global economy means 
            fewer entry barriers for the MNC brands!<br>
            <br>
            *A brand is designated as champion based on a numerical model 
            consisting of a ratings index which takes into account parameters 
            including market share dimension, product perception and economic 
            performance.<br>
            <br>
            **OER considered the brands from fast moving consumer goods (FMCG) 
            and consumer durables segments only for the story. The name of the 
            brands and companies mentioned in the story is only indicative and 
            not exhaustive.</font></p>
