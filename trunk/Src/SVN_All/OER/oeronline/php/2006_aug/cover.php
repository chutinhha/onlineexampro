            <font face="Verdana" size="1"><b>‘Enhancing Competitiveness Instead 
                  of Protection’</b><img border="0" src="cover_6.jpg" align="right" hspace="5" vspace="2" width="150" height="223"><br>
                  Manal Mohammad Al-Abdwani, Director General of Planning and 
                  Follow-up, Ministry of Commerce &amp; Industry and Head of 
                  National Products Promotion Committee, discusses with Akshay 
                  Bhatnagar the initiatives taken by MoCI — in conjunction with 
                  stakeholders — to promote Omani brands<br>
                  <br>
                  Tell us about the origin and progress of the ‘Omani Products 
                  Promotions Campaign’.<br>
                  <br>
                  We started the campaign in 1997 by organising mobile 
                  exhibitions across Oman and undertook different activities to 
                  promote Omani products in the subsequent years. The turning 
                  point came in 2003 when we decided to review our modus 
                  operandi. We consulted companies from 50 different industries 
                  on the specific promotional elements, to engage the consumers 
                  to promote Omani products. Based on the feedback and our 
                  understanding, we targeted three segments — government 
                  authorities, to facilitate and influence them to purchase 
                  Omani products; school children, to make them aware of the 
                  Omani products; and general public at large, through 
                  exhibitions at hypermarkets and various other activities like 
                  advertising, radio programmes etc. The ministry and private 
                  sector companies jointly fund the exercise. We are going to 
                  launch an aggressive three-month-long campaign in September 
                  this year. All the possible communication channels will be 
                  activated to push the message.<br>
                  <br>
                  <b>What is your budget for promotion in 2006?</b><br>
                  We have allocated RO110,000 for the campaign this year. Around 
                  60-65 per cent of the investments will be made by the private 
                  sector through sponsorships. Oman Mobile, Omzest Group, Daleel 
                  Petroleum, Al-Nasr Marble, Oman International Bank, Oman 
                  Emirates, National Detergents and Ali Shaihani Group are the 
                  sponsors of the campaign. There is a special sponsors’ 
                  committee which will liaise with the executive office for the 
                  campaign on a monthly basis to manage the campaign activities.
                  <br>
                  <br>
                  <b>Are you planning to tap Internet also?</b><br>
                  Yes, we are looking at developing a portal which will act like 
                  a digital catalogue showcasing the range of Omani products.
                  <br>
                  <b><br>
                  What made you undertake the consumer perception study and how 
                  was it done?</b><br>
                  The objective was to assess the performance of Omani products 
                  in the local market. We appointed Advanced Business 
                  Consultants to undertake this survey. A sample of 38 products 
                  was selected by MoCI after a preliminary survey. And ABC 
                  contacted around 1,000 people across the Sultanate — drawn 
                  from various categories including consumers, manufacturers, 
                  dealers, retailers, consultants, contractors, and 
                  institutional and industrial users. Thereafter, ABC submitted 
                  the data, analysis and recommendations to MoCI. The entire 
                  exercise took three months. The study focuses on aspects like 
                  product’s market share, ability to face competition, image 
                  perception, packaging, availability in market, price, quality 
                  and even respective company’s financial performance also 
                  studied. It also looks at the success factors of certain 
                  products which are termed Champions so that others could draw 
                  inspiration from their success.<br>
                  <br>
                  <b>When do you plan to release the study findings?</b><br>
                  We are going to disseminate the information at two levels. We 
                  will share the executive summary with the general public 
                  including media expectedly in September. The detailed analysis 
                  will be shared with the respective industries on a one-to-one 
                  basis. <br>
                  <br>
                  <b>As the report also gives recommendations (see box - 
                  Proposed Interventions) on what should be done to push Omani 
                  products, how do you plan to take them forward?</b><br>
                  The recommendations will be discussed among the stakeholders 
                  including the ministry, committee members and the industry 
                  bodies and players. It is important that we involve the 
                  industry as many recommendations are meant for the companies 
                  to follow. For example, the report says the campaign’s role 
                  has to be enhanced. To do that, the industries would be 
                  required to provide more support. <br>
                  <br>
                  <b>How do you plan to protect the interests of Omani brands 
                  with Sultanate’s integration with global economy by way of WTO 
                  membership and impending US-Oman FTA? </b><br>
                  There is no protection for the Omani brands. We want to propel 
                  Omani brands by way of enhancing their competitiveness in 
                  quality, price, presentation and availability. We cannot 
                  provide protection anymore. The government is taking an active 
                  role in ensuring Omani brands’ promotion, and developing the 
                  management capability of the Omani companies. We are 
                  collaborating with the private sector to make sure that Omani 
                  companies become strong enough to face the competition from 
                  other countries. The consumer perception study has 
                  strengthened our belief that Omani brands are capable of not 
                  only succeeding in Oman, but also challenging the 
                  international brands in their own territories.</font></td>
