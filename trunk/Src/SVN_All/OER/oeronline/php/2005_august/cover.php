            <p align="justify"><b>
            <font color="#004000" face="Verdana" style="font-size: 14pt">
            Oman – Is it ready to roll?<br>
            </font><font color="#004000" face="Verdana" size="1"><br>
            With more than 192 countries jostling for space in the global 
            market, Akshay Bhatnagar reviews how Oman is looking to carve a 
            niche for itself by creating a distinct brand.</font></b><font face="Verdana" style="font-size: 10pt"><b><br>
            </b></font><font face="Verdana" size="3"><b><br>
            </b></font><font face="Verdana" size="1">
            <img border="0" src="cover_main.jpg" align="right" hspace="4" vspace="1"></font><font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1">n the last few years, 
            there has been a sudden trend among the Middle East countries to 
            differentiate themselves from each other. On one hand, we have Dubai 
            making its presence felt with its world city image, giving Hong Kong 
            and Singapore a run for their money. Then we have Bahrain, 
            projecting itself as the financial hub and Jordan, creating a niche 
            for itself as the telecommunications heaven. Saudi Arabia is looking 
            at tourism in a big way and Qatar is emerging as the hydrocarbons 
            capital of the region. <br>
            <br>
            This branding rush among the Middle East countries has placed Oman 
            in a quandary. So far known as the ‘Essence of Arabia’, Oman is 
            finding itself trapped in an image which nation branding experts 
            term as outdated and old-fashioned. Even the business community and 
            people at large within the country feel that it’s time Oman projects 
            its real face to the world.<br>
            <br>
            <b>Leave ‘Essence of Arabia’ tag?</b><br>
            <img border="1" src="cover_main1.jpg" align="right" hspace="4" vspace="2">“We need to move on from the image of essence of Arabia. Not many 
            co-relate Oman with essence of Arabia. What’s it? A perfume? Does it 
            mean that we epitomise essence of Arabia more than anybody else?” 
            questions Pankaj K. Khimji, Partner, Khimji Ramdas Group. <br>
            <br>
            Similar feelings are expressed by HH Faisal Bin Turki Al-Said, Head 
            of Planning &amp; International Relations, The Omani Centre For 
            Investment Promotion &amp; Export Development (OCIPED), as he says, “The 
            western world still perceive essence of Arabia as camels, deserts 
            and Arabian nights. Oman offers much more than this. Calling Oman as 
            essence of Arabia is not wrong but it is an understatement also as 
            the country has come up a long way on the socio-economic development 
            and tourism infrastructure fronts.” <br>
            <br>
            In fact, the general opinion is that not many outside Oman knows 
            that the country stands for the essence of Arabia. How far it is 
            true is a big question mark? But the Government of Oman, under the 
            wise leadership of His Majesty Sultan Qaboos bin Said, has already 
            taken active steps to reassess the branding of the country. Two 
            major initiatives have been taken in this direction. First, a high 
            level Oman Branding Committee has been formed. The Committee headed 
            by HE Maqbool bin Ali Sultan, the Minister of Commerce and Industry, 
            comprises of Under-Secretaries from the relevant Ministries as in 
            Tourism, National Economy, Information and Transport &amp; 
            Communications, along with the President of the Oman Chamber of 
            Commerce and Industry and the executive President of OCIPED. The top 
            executives from the business community are also part of this 
            Committee. <br>
            <br>
            On the task before the Committee, HE Maqbool says, “This project 
            comes as part of our vision of developing a non-oil based 
            sustainable economy and promoting the image of Oman to the world, 
            not only as a tranquil, authentic Arabian tourism experience, but as 
            a hub for investment, technology, trade and education for the whole 
            peninsula.”<br>
            <br>
            As a second step, Oman has appointed global branding consultancy 
            major, Landor Associates, to project Oman in the right perspective 
            in the international market through a global branding campaign. 
            Landor has been credited with destination branding campaigns across 
            the world including Hong Kong, Jordan and Al Ain in the UAE. It is 
            also advising on promoting tourism in Saudi Arabia. <br>
            <br>
            As per the information with OER, Landor has recommended Oman to shed 
            the tag of being the essence of Arabia and rebrand itself. What is 
            going to be the new image of Oman has not been decided so far. As 
            informed by Landor, the agency is expected to present its key 
            findings on Oman soon, which might lead to the new branding of the 
            country. <br>
            <br>
            Talking about the strategy of Landor, Andrew Blakemore, Nation 
            Branding Specialist, Landor Associates Dubai, informs, “Our aim is 
            to create a long-lasting image of Oman in people’s mind. It is 
            clearly a unique country and will have enormous appeal to markets on 
            a global scale. But it needs to be promoted in a way that is 
            consistent and sustainable. The key is to position Oman, over time, 
            in a way that is relevant to many different audiences, but is 
            equally based on values that are true to the national identity.” <br>
            <br>
            <b>Rebranding of a nation is not easy</b><br>
            The branding of a nation is an extremely tough task. It’s much more 
            difficult than branding a product or a service. “The principles of 
            branding apply in equal measure to countries as they do to 
            corporations. But methods are different. The countries will compete 
            daily with neighbours or block regions for tourism, inward 
            investment and export sales,” says Charles Brymer, Chairman &amp; Chief 
            Executive, Interbrand Group.<br>
            <br>
            He further adds, “You can’t impose branding on a country, but you 
            can find out the aspects that appeal to the widest group of people 
            and which are different and special. Take time, be consistent, build 
            an integrated picture and always back it with quality. You will have 
            success.”<br>
            <br>
            Philip Kotler, the renowned management guru, agrees with Brymer. 
            Kotler believes, “A new product comes into existence to which a set 
            of associations can later be mapped. Even after a product has been 
            launched, companies are free to make modifications in response to 
            consumer demand. The countries, however, may be more limited in 
            altering their place brands. Obviously a country can’t replace its 
            beaches with mountains, or grow bananas if its climate favours snow. 
            Although it may be possible for a nation to attract more foreign 
            direct investment or shift its economic base, there will always be 
            some constraints over which it will have little or no control.”<br>
            <br>
            No matter what management experts say, but there have been instances 
            where countries have successfully changed their branding over a 
            period of time. The most shining example of nation rebranding is 
            Singapore, a highly developed nation, which is smaller than many of 
            the big cities in the world. In 1966, when Lee Quan Yew took the 
            reigns of the country in his hands, Singapore was a heaven for 
            smugglers, criminals, sailors and adventurers and was beset with 
            numerous cultural and economic problems. He transformed the country 
            by imposing law and order and focused on achieving a high education 
            level for his countrymen. Today, the results are there for everyone 
            to see. <br>
            <br>
            To an extent, India is another example. It has moved away from being 
            known as a land of snake charmers, widespread poverty and Taj Mahal. 
            Today, it is the hub of information, communication and technology 
            (ICT) with most of the multi national companies like Microsoft, IBM, 
            Oracle, QuarkXpress, Google and Reuters etc. setting up bases there.
            <br>
            <br>
            <b>Strengths of Brand Oman</b><br>
            Brymer says focus on the unique aspects of the country. So what are 
            the unique strengths of Oman which gives it the competitive 
            advantage over other country brands? Different people have different 
            views based on their area of activity and personal experience. But 
            broadly speaking, five distinct features separate Oman from other 
            countries, especially its Middle East neighbours when we talk of 
            product differentiation in management parlance:<br>
            <br>
            <b>1 Location</b><br>
            Oman’s strategic location makes it a gateway linking trade between 
            the east and the west. Lots of companies have made Oman their base 
            purely for its unique location, which makes it easy for them to 
            carry on their trade. “Oman offers direct access to the Indian Ocean 
            and it has got a very good location in terms of reaching out to the 
            ports in Europe and Asia,” says Tony Kinsman, Chief Executive 
            Officer, Sohar Aluminium<br>
            <br>
            HH Faisal adds, “The economy is growing with Oman making good use of 
            Salalah Port, Sohar Port and the Sultan Qaboos Port. More 
            investments are being pumped in expanding and further modernising 
            the existing ports and developing new ones. If Oman has to build on 
            its strength, it has to be its location… and concentrating on it 
            will lead to development in other potential industry sectors 
            automatically.” <br>
            <br>
            <b>2 Rich &amp; Friendly Culture</b><br>
            Perhaps, this is the most understated and important aspect of Oman 
            which should be projected to the outside world. Due to lack of any 
            major distinct identity, Oman is perceived as any other Middle East 
            country, which could be conservative and unfriendly. But the reality 
            is totally different from the perception.<br>
            <br>
            Foreigners who set their foot in Oman for the first time are 
            surprised by the friendliness of the local people. Oman is a liberal 
            and open society, which respects traditional values, but at the same 
            time has adapted to the needs of the modern world. Women are treated 
            as equal in the real sense, unlike many other Middle East countries.
            <br>
            <br>
            Talking of Oman, David W. Robinson, Regional Manager – Mining &amp; 
            Metals, Saudi Arabia &amp; Gulf States, Bechtel International, says, 
            “Oman has a great business environment. It is a very friendly place, 
            both from the business as well as cultural aspects.”<br>
            <br>
            Shedding more light on the welcoming culture of Oman, Blakemore 
            says, &quot;It's quite clear talking to anyone who has visited Oman or 
            done business there that it has a character all of it's own. It is 
            also blessed with many natural assets, and a way of interacting on a 
            human level, that we know from experience, can have universal 
            appeal. Having said that, early in our study we found that it isn't 
            necessarily a case of needing to shift perceptions of Oman… beyond 
            the region there isn't much accurate knowledge of the country in the 
            first place. That's what we need to work on.&quot;</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>3 Diversity</b><br>
            Spread over 309,500 sq kms, Oman is the second-largest country among 
            the Gulf countries. Oman’s rich diversity in terms of natural beauty 
            and historical monuments is unparalleled in the entire Gulf region. 
            Oman holds tremendous potential in terms of tourism with its assets 
            like fascinating forts, virgin beaches, beautiful landscapes, wadis, 
            traditional and modern souqs and Frankincense, besides the jewel in 
            the crown, the serene Salalah. In 2004, more than 1.2 million 
            tourists visited Oman. <br>
            <br>
            Mohammed bin Hamoud bin Zahir Al Toobi, Undersecretary, Ministry of 
            Tourism, says, “When people read about Oman and see its pictures, 
            they find it different and authentic. It’s not artificial and fake. 
            They find nice beaches, rigid mountains and resorts. On the other 
            hand, there are sands, deserts, greeneries, historical monuments and 
            adventure activities etc. So they find that Oman is a land of 
            diverse offerings to suit the needs of all kind of tourists.” <br>
            <br>
            “Muscat airport has a passenger turnover of about 3.5 million 
            passengers a year. The Oman Brand is a greater draw in Europe than 
            might be known. Oman and Oman Air always receive an excellent 
            reception from tour operators and buyers when it attends the great 
            travel shows in London, Berlin and Dubai,” says Edward Grauvogl, 
            Divisional Manager - Planning, Oman Air. <br>
            <br>
            <b>4 Economic Progress</b><br>
            Under the wise leadership of His Majesty Sultan Qaboos bin Said, 
            Oman has made remarkable economic progress. The country had a gross 
            domestic product of $28.78 billion in 2004. Several multi national 
            companies are investing in and setting up bases in the country. On 
            the tourism front, multi billion dollars projects like Shangri-La 
            Barr Al Jissah, the Blue City and the Wave are coming up, which are 
            going to give a major thrust to the sector. The other major 
            development projects like Oman-India Fertiliser, Sohar Refinery, 
            Oman Polypropylene Project, Tri-Services College, Salalah Port 
            expansion, Seeb/Salalah airport expansions etc. are underlining the 
            emergence of Oman as a major economic power in the region. <br>
            <br>
            Not to be left behind in the ICT sector, Oman has developed the 
            technology park ‘Knowledge Oasis Muscat (KOM)’ to meet the current 
            and future requirements of high technology-dependent business, 
            expanding local businesses and attracting more international 
            investments. A number of multinational companies have already 
            established a presence at KOM. <br>
            <br>
            <b>5 Political Stability</b><br>
            The socio-economic progress of the country has been possible largely 
            because of the political stability in Oman. Unlike many other Middle 
            East countries which are mired in political instability, Oman faces 
            no such issues. This is very important from the foreign investor’s 
            perspective. Multi national companies know that Oman is a 
            politically stable nation with full membership of the World Trade 
            Organization (WTO) and their investments will be completely safe in 
            the country. <br>
            <br>
            As the recognition of Oman is not very high in the international 
            market, lot of potential investors may not realize that it is such a 
            stable nation. “It’s important for Oman to be separated from the 
            broad perception of Middle East region. Outside people should 
            recognize that Oman is a very stable area and it is a long way from 
            the unstable areas,” says Kinsman of Sohar Aluminium.<br>
            <br>
            It is good that Oman has so many strengths, but for branding 
            purpose, the country has to identify one or two of its strengths. If 
            we try to push too many messages in our communication, our target 
            audience could get confused and we might be back to square one. We 
            already have a good product in hand, it’s a question of coming up 
            with a focused communication in a forceful, uniform and constant 
            manner. As Pankaj K. Khimji says, “In terms of branding, we need to 
            narrow down our approach to one or two strengths only, say tourism 
            or economic activity.” <br>
            <br>
            <b>Responsibility of branding</b><br>
            Though Oman is still in the process of identifying its perfect 
            branding proposition, it is imperative to understand early on about 
            who should be made responsible for promoting Brand Oman. As per the 
            indications in media, once the new branding strategy takes shape, 
            the Government is keen to provide a branding kit to the relevant 
            stakeholders comprising of Ministries, industry bodies and business 
            groups, which will form the basis on which they would be conducting 
            their brand promotion exercises. <br>
            <br>
            Of course, the Government of Oman is going to be primarily 
            responsible for pushing brand Oman, but the corporate sector and 
            general people should also join hands to partner the Government in 
            making Brand Oman a roaring success. Blakemore says: “Branding a 
            country requires a great deal of collaboration and cooperation 
            between many different stakeholders.” <br>
            <br>
            It’s not a question of pushing a symbol or a slogan through 
            marketing campaigns, it’s a question of realizing the importance of 
            the branding exercise and taking up responsibility on our own to 
            make it a success. Besides the regular ads and events, the 
            Government could embark on a global diplomacy exercise to take the 
            message to the far corners of the world. The corporate sector should 
            integrate the essence of brand Oman in their promotion campaigns to 
            provide a uniform picture of Oman to the world. They should continue 
            to follow the best business practices while conducting business 
            abroad thus reinforcing Oman’s professional business environment.
            <br>
            <br>
            And most importantly, it is the people of a country who are the most 
            powerful and visible brand ambassadors. When a foreigner arrives in 
            Oman, his first point of interaction with the country is with the 
            airport staff, visa procedures, cab drivers, hotel employees, 
            shopkeepers etc. The first impression is the last impression. If the 
            first impression was bad, then no matter how much we expose them to 
            our world-class service, they would still carry a wrong picture of 
            the country. And there can’t be any bigger promotion than word of 
            mouth publicity. Perhaps an extensive exercise to educate the public 
            about the importance of their role in projecting the right image of 
            the country is worth the investment. <br>
            <br>
            Taking a cue from Dr. Mahathir Bin Mohamad of Malaysia who had first 
            coined and used the term ‘Inc.’ in context of a country, Pankaj K. 
            Khimji says, “To realize the true potential of Oman as a brand, we 
            need to start marketing the country as Oman Incorporated. Our 
            ambassadors in embassies abroad need to start marketing Oman as the 
            chief marketing officers of Oman Inc.” </font></p>
