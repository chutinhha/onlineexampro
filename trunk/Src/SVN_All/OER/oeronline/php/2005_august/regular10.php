            was associated with a couple of leading stock broking firms in 
            senior management capacities. Following an approach from the Board 
            of GIS, I returned to GIS earlier this year as its Chief Executive 
            Officer.<br>
            <br>
            <b>Please enlighten our readers about the management structure of 
            your company</b><br>
            GIS has a very flat management structure. We offer broadly four 
            services: Stock Broking, Corporate Finance, Funds management and 
            Portfolio Advisory Services. In addition we have our own proprietary 
            investment portfolio. We have strong research and execution 
            capabilities for brokerage business. Each of the service divisions 
            as also the operations and finance functions are headed by qualified 
            and experienced managers who report to me and are in turn assisted 
            by support staff. <br>
            <br>
            <b>What does your role in the company entail?</b><br>
            As CEO, I’m responsible for the management of the company as a 
            whole. I am responsible to the Board of Directors of GIS for 
            devising business strategies, formulation of annual business plans; 
            and achieving business and revenue targets. I am also responsible 
            for strategic initiatives related to expansion and diversification 
            of business of the company. <br>
            <br>
            <b>Briefly describe your management style:</b><br>
            I like to lead by example. I think a CEO should have both the vision 
            to strategise as well as the management capability and expertise to 
            drive the business forward. These virtues make him in an ideal 
            person to guide and lead the team to deliver the business goals. As 
            a CEO, I like to provide the direction and leave the managers to 
            manage their functional responsibilities. I believe in a strong 
            Management Information System (MIS) which should provide me the 
            requisite information about the progress of each business area and 
            set up a mid-course correction strategy, if required. <br>
            <br>
            I prefer to concentrate more on strategic initiatives that we need 
            to take to expand and diversify our business. Also human resource 
            development is another area where I take direct interest, as I am 
            very keen to ensure that our staff acquires the relevant knowledge 
            base and experience to become competent managers in due course. <br>
            <br>
            <b>What are the challenges that your business faces in Oman? How do 
            you propose overcoming these challenges?</b><br>
            In a dynamic local and regional environment, our challenge is to 
            perform well on the investment front and stay ahead of the 
            competition in service areas. As far as investment portfolio is 
            concerned, capital markets in Oman and rest of the GCC region, where 
            our investments are concentrated, are performing well and the 
            challenge is to ensure that one does not get carried away. With 
            respect to services, there are a number of strong brokerage and fund 
            management businesses in Oman and in the GCC region. My challenge is 
            to formulate strategies to increase our market share in each of the 
            service area in which we are involved. This we intend to do through 
            a strong research focus and high quality execution capabilities. Our 
            long-term strategy is to become a strong regional player in our 
            businesses through expansion and strategic alliances. <br>
            <br>
            <b>
            <img border="0" src="athe1.jpg" width="120" height="101" align="left" hspace="5">How 
            has the investment services sector in Oman evolved in the past few 
            years?</b><br>
            The market has progressed well in the last 10 years. In the initial 
            phase, we just had Muscat Securities Market (MSM) that performed the 
            roles of exchange, depository and the regulator. In 1998, the 
            functions were separated. The Capital Market Authority (CMA) was 
            established while the Muscat Security Market retained the role of 
            stock exchange. A separate depository too was established. <br>
            <br>
            This has enabled Oman to have the best-regulated capital market in 
            the GCC region; thereby enhancing the confidence of international 
            investors. Over the years, we have seen increasing portfolio 
            investment by foreign investors, despite the fact that in terms of 
            market capitalization and turnover, it does not match up to Saudi 
            Arabia, Kuwait or UAE. With the listing of Omantel, not only our 
            market capitalization will increase significantly but the volumes 
            too will receive a considerable boost. As it is, nowadays the 
            volumes are much better than what it used to be four or five years 
            back. The month of June has been very good in volume terms. <br>
            <br>
            What we need is a few more good quality large stocks such as Omantel, 
            which will enable MSM to further improve its market capitalization 
            and turnover. <br>
            <br>
            The bond market is still in its infancy. Once again we need the 
            orderly development of this market through listing of more 
            Government and Corporate bonds. There could be even hybrid issues 
            such as fully or partly convertible bonds with put and call options.
            <br>
            <br>
            We also hope to see the cross listing of securities from other 
            markets and in due course, the integration of the regional stock 
            markets into a GCC common market. This augurs well for both 
            investors as well as investment and stock broking firms. <br>
            <br>
            <b>Please throw some light on how Gulf Investment Services compares 
            with other competing players in Oman.</b><br>
            GIS is an investment-cum-financial services company and can be 
            compared with just a couple of similarly placed companies. Then 
            there is little pure brokerage business. In terms of stock broking 
            volumes, we are among the top five or six companies. Over a period 
            many of the top stock broking firms have moved out and established 
            relationships in the GCC region. Today the market is driven as much 
            by funds flowing in from the GCC region as much by local 
            institutional and retail investors. So in that kind of a scenario 
            those who have established relationships within the GCC region and 
            with high net worth investors in Oman are performing better. GIS has 
            embarked on a concerted marketing strategy and a focused business 
            development approach to garner a larger market share for itself. <br>
