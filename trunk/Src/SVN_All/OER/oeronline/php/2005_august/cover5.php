            <p align="justify"><b>
            <font color="#004000" face="Verdana" style="font-size: 14pt">
            ‘One image of Oman will be reflected soon’<br>
            </font><font color="#004000" face="Verdana" size="1"><br>
            In an exclusive dialogue with Akshay Bhatnagar, Mohammed bin Hamoud 
            bin Zahir Al Toobi, Undersecretary, Ministry of Tourism, talks about 
            the rebranding exercise of Oman besides the strategy for the growth 
            of tourism in the country<br>
            </font></b><font face="Verdana" size="3"><b><br>
            </b></font><font face="Verdana" size="1"><b>
            <img border="1" src="cover5.jpg" width="130" height="131" align="left" hspace="4" vspace="1">How is tourism industry 
            shaping up in Oman?</b><br>
            The tourism industry in Oman is growing rapidly. Tourism is the 
            fastest growing sector globally also. The Ministry of Tourism has 
            been promoting Oman within the country as well as internationally. 
            The thrust is on organizing the sector by setting up world-class 
            infrastructure first as a foundation to make Oman the most desired 
            tourist destination. Once we are fully confident that our 
            infrastructure is completely in place, we will market the country 
            far more aggressively than ever before. We have already done most of 
            the work in this direction by introducing new rules and regulations 
            through legislations and setting-up of the infrastructure. We have 
            already relaxed a number of visa and other procedures and provisions 
            to make it easier for tourists to come here. We have also introduced 
            many incentives for attracting more investments in the tourism 
            sector.<br>
            <br>
            All these efforts are yielding good results. Based on our tracking 
            of business done by tourist facilities like travel companies, 
            hotels, motels, guesthouses and hotel apartments, besides the inflow 
            of tourists, we find that the tourism industry in Oman is 
            progressing at a very good rate. More numbers of Omanis are also 
            getting into the tourism-related services. <br>
            <br>
            <b>What is the size of tourism industry in Oman?</b><br>
            Tourism sector contributes almost one per cent to the nation’s gross 
            domestic product. (as per Ministry of National Economy, Oman’s GDP 
            in 2004 was $28.78 billion, which means tourism sector size is 
            $287.8 million). The industry is growing at a very fast rate. 
            Honestly speaking, we have achieved better results than the targets 
            we had set for ourselves. We have exceeded our own expectations in 
            terms of revenues and tourist traffic.<br>
            <br>
            <b>How many tourists visited Oman in year 2004? </b><br>
            Last year, we had more than 1.2 million inbound tourists. They come 
            for both leisure as well as business. Oman attracts all kinds of 
            tourists. They come in to visit their friends, for entertainment, 
            sun and beaches, historical monuments, geological beauty and 
            adventure activities like wildlife and bird watching, trekking, rock 
            climbing and diving etc. Oman has a lot to offer to suit the varied 
            needs of the tourists. <br>
            <br>
            <b>Which countries contribute maximum tourists to Oman?</b><br>
            Most of the tourists come from Arabian countries, besides Britain 
            and Germany.<br>
            <br>
            <b>…Are you marketing Oman more aggressively in Britain and Germany 
            compared to other parts of the world?</b><br>
            We are marketing Oman quite well in Germany and Britain. We take 
            part in the international tourism exhibition called ‘ITB Berlin’ in 
            Germany every year. It is the largest tourism-related exhibition in 
            the world. We do promotions in Germany through other means also. 
            With their fondness for traveling, perhaps, Germany must be among 
            the select group of countries which have maximum outbound tourists.<br>
            <br>
            <b>When you go out of Oman to market the country in the 
            international market, what feedback you get in terms of perception 
            of Oman?</b><br>
            The awareness about Oman is there. You find Oman highlighted in 
            Internet, press ads and TV channels like Discovery and National 
            Geographic. We also take part in most of the major tourism-related 
            exhibitions across the world. This exposure has created a lot of 
            awareness about Oman. When people read about Oman and see its 
            pictures, they find it different and authentic. It’s not artificial 
            and fake. They find nice beaches, rugged mountains and resorts. On 
            the other hand, there are sands, deserts, greeneries, historical 
            monuments and adventure activities etc. So they find that Oman is a 
            land of diverse offerings to suit the needs of all kinds of 
            tourists. <br>
            <br>
            Do they get surprised to know that Oman offers so much and they 
            never knew about it?<br>
            Yes, the country has diverse tourist attractions. Besides diversity 
            in geology, there is diversity among people also with so many 
            cultures, customs and traditions being followed here. You will find 
            different kinds of weather also. Muscat is hot, but Jabal Akhdar are 
            pleasant. Salalah is lush green and rainy now.<br>
            <br>
            <b>Where do we stand in terms of tourism compared to Dubai? </b><br>
            Of course, Dubai gets far more tourists than us. But the comparison 
            between the two is unfair as they are totally different kinds of 
            markets. Every country has its own character and identity. So, 
            Oman’s character and identity is different from Dubai.<br>
            <br>
            <b>As Dubai attracts more tourists, are we marketing Oman to those 
            also who are coming to Dubai through some joint promotion package 
            campaigns etc.?</b><br>
            Well, we have done many joint promotion campaigns in alliance with 
            other GCC countries. Most of them have been done with Dubai and 
            Qatar. That’s the reason why we have joint visa arrangements with 
            Dubai and Qatar. A visa to Oman means a person need not get a 
            separate visa for Dubai and vice versa. We are trying to market the 
            region as one destination. Instead of competing with each other, the 
            focus is on complementing and integrating each other. <br>
            <br>
            <b>How far has it worked for Oman?</b><br>
            The arrangement has yielded good results not just for Oman, but for 
            other countries also. This has definitely contributed to the 
            increase in tourism in Oman. <br>
            <br>
            <b>What is your strategy to expand tourism sector as per the 
            expectations of Oman’s vision 2020?</b><br>
            We are enjoying this challenge of taking tourism to the next level. 
            We have already exceeded the targets set for ourselves last year as 
            I told you. We already have strategies in place to market tourism. 
            Every 2-3 years, we review and evaluate them. Wherever we find a 
            weakness, we improve ourselves immediately. We are thorough in our 
            market studies and strategies. <br>
            <br>
            We have our own way of marketing the country. We don’t exaggerate. 
            We do things in a balanced way. There is a balance between the 
            promises made in our marketing campaign and the support 
            infrastructure we have. We don’t want to market the country in a 
            manner, as if we have hotel facilities for 5-6 million tourists. We 
            don’t have such facilities. The more infrastructure and facilities 
            we add, the more we will market ourselves. There has to be a balance 
            between the customer’s expectation and satisfaction level.<br>
            <br>
            <b>What is the role of multinational companies in expanding tourism 
            in Oman?</b><br>
            We have lot of multinational companies doing consultancy, planning 
            and marketing for us. There is a high level of co-operation between 
            us. We also use their expertise to give shape to our strategies and 
            vision for the future growth path. <br>
            <br>
            <b>Which are the major projects that are going to push tourism in 
            Oman?</b><br>
            The Wave, the Blue City and Barr Al Jissah are the big projects 
            which are coming up. Barr Al Jissah resort is going to be the 
            fastest off the block. It consists of three state-of-the-art hotels. 
            They will have all the world-class facilities like spas, health 
            clubs, restaurants, heritage villages… you name it and they have it. 
            The first stage of the resort is expected to be operational by the 
            end of this year. It will boost the tourism in the country.<br>
            <br>
            The other two projects, the Wave and the Blue City, are almost like 
            cities on their own. You can imagine what a city consists of and 
            they will have it all - hotels, motels, entertainment centres, 
            shopping malls, hospitals, schools, museums, aquariums, marinas, 
            health centres, spas etc. They are going to be a major hub for 
            tourists. Each of them has its own concept and theme targeted at 
            specific customer base. They will complement and integrate rather 
            than compete with each other.<br>
            <br>
            <b>The national carriers play an important role in marketing their 
            home countries. How far has Oman Air been successful in playing that 
            role for Oman?</b><br>
            Well, Oman Air has done a very good job. It has achieved a lot on 
            the promotion and marketing front in its role of the national 
            carrier for Oman. But of course this is not the end. The Government 
            and the Ministry would like to see even better and better results 
            than what we get right now even though Oman Air is doing a good job 
            and we are very happy with it. Every year Oman’s Air’s top 
            management proposes good strategies and ideas to take the airline 
            forward. I hope Oman Air will carry this good performance and 
            contribute a lot more to the country.<br>
            <br>
            <b>What are the lessons we need to learn from our neighbours in the 
            Middle East on the tourism front?</b><br>
            Life is a school for everybody and a person is never born 
            intelligent. You learn from people who are older or more experienced 
            than you. You take the good things from them. Sometimes it is nice 
            to come late, as you learn from what has been done by others 
            already. That is the case with Oman. We are not late as we had 
            realized the potential of tourism and started working on this front 
            years back. We have now reached the maturity stage. We do monitor 
            the other tourist markets, especially in our neighbourhood. We take 
            good ideas from them and they also take good ideas from us.<br>
            <br>
            But again, it depends on your own assets and potential. It depends 
            on what you are offering and what kind of tourists you would like to 
            have in the country. Ours is a very selective and organised kind of 
            tourism. We are trying to keep it this way so that we don’t affect 
            the authenticity of the country. We don’t want to change the norms, 
            culture, tradition, handicrafts and friendliness of the Omani 
            people. We are trying to grow our tourism in a manner so that our 
            people are not affected by it in any negative manner. If we don’t 
            try to preserve the essence of Oman and do the opposite of what we 
            are trying to do, the chances are that the tourists may not find 
            anything unique here.<br>
            <br>
            <b>We believe an Oman Branding Committee has been set up with the 
            Tourism Ministry as an integral part of it to strengthen ‘Brand 
            Oman’. Can you tell us the progress made by the committee so far?</b><br>
            Yes, Oman Branding Committee has been set up to look into the issues 
            related to the branding of the country. We hope that very soon one 
            theme and image of Oman would be selected and reflected everywhere. 
            All the sectors in the country would speak in the same language when 
            talking about Oman. We have been doing it already and we would do so 
            again in the future in a much stronger way. The country will be 
            marketed as one product. This will help us boost the marketing of 
            the country not just in tourism, but other sectors also.<br>
            <br>
            <b>How soon do you think this single theme for Oman will get 
            finalized?</b><br>
            I have no idea, to be honest with you. Because there are lot of 
            Government bodies involved in the whole exercise. Tourism Ministry 
            alone is not responsible for this as it relates to all other 
            industry sectors and Ministries also. But I believe the process is 
            on a fast track. </font></p>
