            <font color="#004000" face="Verdana" style="font-size: 14pt">
            Sweden-Top Nation Brand <br>
            </font><font color="#004000" face="Verdana" size="1"><br>
            Simon Anholt, an advisor on public diplomacy to British Government, 
            in an exclusive write-up for OER, focuses on the branding index of 
            nations and the opportunities for lesser-known nations to improve 
            their brand equity</font></b><font face="Verdana" style="font-size: 10pt"><b><br>
            </b></font><font face="Verdana" size="3"><b><br>
