                <font face="Verdana" size="1"><b>
                <img border="1" src="cover3.jpg" width="100" height="138" align="left" vspace="2" hspace="5"></b></font><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">oday, we live in a world, 
            which globalisation has turned into a single marketplace. In this 
            marketplace, every country suddenly needs to compete with every 
            other for its share of the world’s consumers, tourists, investors, 
            and for the attention and respect of the international media, of 
            other governments, and the people of other countries. <br>
            <br>
            In such a busy and crowded marketplace, branding the nation is 
            virtually an act of self-defence. This is because most people simply 
            don’t have the time to learn what countries are really like - we all 
            navigate through the complexity of the modern world, armed with a 
            few simple clichés about countries: France is about fashion, Italy 
            about style, America about power and money, Germany about cars and 
            engineering, Japan about miniaturisation, Russia about corruption.
            <br>
            <br>
            In other words, we all think about countries in the same way as we 
            think about brands in a supermarket. These simple clichés - whether 
            they are positive or negative, relevant or irrelevant - can truly 
            make the difference between success and failure in every aspect of a 
            nation’s international undertakings. <br>
            <br>
            We may not like this, but there’s little we can do to change it. 
            It’s very hard for a country, even a rich and powerful country like 
            America, let alone a little-known country like Oman, with less 
            presence in the international media, to persuade people in other 
            parts of the world to go beyond these simple brand images and start 
            to understand the real complexities, the contradictions and the 
            cultural and social riches which lie behind them. <br>
            <br>
            So it becomes the primary responsibility of governments to find out 
            what their national brand image is, and to develop a proper strategy 
            for managing it: it is their responsibility to build a nation brand 
            that is fair, true, positive, attractive, memorable, genuinely 
            useful to their economic, political and social aims, and honestly 
            reflects the spirit, the genius and the will of the people. <br>
            <br>
            Managing the national brand has indeed become one of the primary 
            skills of governments in the twenty-first century.<br>
            <br>
            Obviously, countries aren’t soap powder. But, just like companies, 
            they depend greatly on their reputation, and must look after it very 
            carefully; it is their most treasured asset. A strong national brand 
            helps to attract investment, talent, consumers and tourists, and 
            enhances the country’s cultural and political influence. <br>
            <br>
            <b>Anholt-GMI Nation Brands Index</b><br>
            Since spring, I have been running a new quarterly survey called the 
            Anholt-GMI Nation Brands Index, together with Global Market Insite 
            in Seattle. This survey is the first to measure, compare and track 
            the brand values of nations. <br>
            <br>
            By studying the results of the NBI, it is now becoming clear what 
            kind of country tends to have powerful brand values, at least in the 
            view of our worldwide panel of 18,000 consumers: a stable, liberal, 
            democratic Western state with a tendency to neutrality, often 
            producing several well-known branded products, and a strong 
            international presence in the media (either through entertainment 
            and culture or through attractive tourist promotion). The countries 
            with the best brands are rich countries, too: the top fifteen 
            countries in the NBI each have a gross domestic product of at least 
            $23,000 per capita, whereas the bottom ten all fall below $19,000. 
            Countries like Canada, Australia, New Zealand and Switzerland appear 
            to carry much of the same appeal which put Sweden at the top of the 
            first Anholt-GMI Nation Brands Index - indeed, our panel seems to 
            consider Australia, Canada and Switzerland as ‘more Swedish than 
            Sweden’. <br>
            <br>
            But the world’s love for Australia and Canada, in some senses, is 
            way beyond reason - or certainly quite hard to account for in 
            practical terms. Unlike Sweden or Germany or Italy, neither country 
            is associated with loved and world-famous brands; unlike Britain, 
            neither has an internationally prominent or respected political 
            figure; neither is an especially prolific or prominent contributor 
            of cultural offerings to the world stage. But both are large, 
            beautiful, relatively remote countries with relatively small 
            populations; they both have a certain exotic appeal, which is helped 
            by the fact that not many people are as intimately familiar with 
            them as, say, Spain, France or America. <br>
            <br>
            It’s certainly easier for countries which are not in the mainstream 
            of global politics, to achieve good brand rankings: negative brand 
            attributes nearly always accrue to first-world countries as a result 
            of unpopular foreign policies (USA’s foreign policy being a case in 
            point), and any country which regularly plays a key role in world 
            affairs, will find this kind of negative equity hard to avoid 
            completely. <br>
            <br>
            The UK is an exception which proves the rule: it is the only country 
            near the top of the NBI to occupy the international political and 
            economic mainstream (it’s the only trillion-dollar economy, the only 
            permanent member of the UN Security Council, and the only nuclear 
            power in the NBI Top Five). To maintain such a positive brand 
            despite these factors - and despite having been an imperial power 
            within fairly recent history - is quite an achievement. <br>
            <br>
            <b>Can the Nation Brand be Managed? </b><br>
            Any discussion about the brand values of nations, about their 
            importance in the modern world and their impact on people’s 
            decisions, inevitably raises the question of whether there is 
            anything that can be done to change them, to reverse a negative 
            image, or simply to manage the brand in the way that better-run 
            corporations sometimes succeed in doing with their brands. <br>
            <br>
            Heavy promotion of the nation’s commercial ‘ambassadors’ such as the 
            national airline, famous branded products and services, as well as 
            tourism, cultural relations, media personalities, politicians, 
            investment opportunities and the people of the country all play 
            their part in creating the brand image of the nation. But in the 
            end, deliberate acts of communication, no matter how well-performed 
            or how well-funded, can only work at the margins of perception, and 
            one simple truth about nation brand emerges from the NBI: a 
            reputation can only be earned. <br>
            <br>
            People form their views about countries in the following ways:<br>
            a. By the things the country does, and the way it does them<br>
            b. By the things the country makes, and the way it makes them<br>
            c. By the way the country looks - or people think it looks<br>
            d. By the way other people talk about it<br>
            e. By the company it keeps<br>
            f. By the way the country talks about itself<br>
            <br>
            There is a common misconception that nation branding is mainly about 
            (f) - in other words, that the image of a country can be built 
            through paid-for communications, through attractive logos and clever 
            slogans. Actually, this is one of the least effective and least 
            reliable ways of communicating the ‘national brand’. Saying things 
            is not enough: they either have to be said by respected, independent 
            voices - (d) - or better still, proven: (a), (b), (c) and (e). <br>
            <br>
            Just as advertising can’t sell a product which doesn’t deliver on 
            its promises or which people don’t need, so a country can’t build 
            its reputation by singing its own praises or spewing out endless 
            information about its wonderful products, investment opportunities, 
            people, places and achievements. In today’s world, information is 
            virtually valueless because there’s so much of it. If someone has 
            already decided to buy, then they will welcome information; if not, 
            they will simply ignore it. <br>
            <br>
            In the end, if a nation wants to change its brand image, it must 
            learn to behave differently. But understanding how it is really 
            perceived by its various audiences and stakeholders, how nation 
            brands are formed and how they change, will at least ensure that the 
            new behaviour will build the new brand as effectively, efficiently, 
            fairly, truthfully, usefully and predictably as possible. <br>
            <br>
            The brand images of countries, whether good or bad, are seldom an 
            entirely accurate reflection of the reality of the country. In the 
            case of developing countries, the most common reason for this 
            disconnect between image and reality is time: the country may change 
            quite quickly, but its image lags behind by years or decades and 
            sometimes even centuries. <br>
            <br>
            It is widely believed that little can be done to correct or enhance 
            a country’s image: one can blame the media, people’s ignorance, 
            globalisation or one’s own history, but apparently not even the 
            richest countries can do much to change a negative stereotype, once 
            it has become ingrained or build a brand for little-known places.
            <br>
            <br>
            But there are enough examples and enough ‘best practice’ - such as 
            Slovenia, Chile, New Zealand, South Africa, South Korea, Ireland, 
            Spain and Australia - to prove that a country’s international 
            reputation can be managed and changed to better represent the 
            current reality and future aspirations of the place, as long as 
            there is a clear strategy for doing so, leadership, and proper 
            coordination between government, the public and private sector, and 
            the population in general. <br>
            <br>
            This message is of critical importance to developing nations, which 
            simply don’t have the time to wait until their image catches up with 
            the rapid pace of their development. For them, place branding 
            represents a way of ensuring that their fundamental human qualities, 
            their efforts, achievements and ambitions are seen, acknowledged, 
            and properly interpreted in their own region and beyond. In a deeper 
            sense, place branding also provides a way for newer, smaller and 
            less well-known countries to establish and broadcast their true 
            cultural, social and historical identity, and carve out a 
            ‘perceptual niche’ for themselves in the global community. <br>
