            3 Principles of Country Branding<br>
            </font><font color="#004000" face="Verdana" size="1"><br>
            Al Ries, the branding guru par excellence, in an exclusive write-up 
            for OER enlightens on the universal application of branding 
            principles and how different countries and cities could brand 
            themselves with special reference to Oman</font></b><font face="Verdana" style="font-size: 10pt"><b><br>
            </b></font><font face="Verdana" size="3"><b><br>
            <img border="1" src="cover1.jpg" width="100" height="123" align="left" vspace="2">T</b></font><font face="Verdana" size="1">he principles of branding 
            can be applied to any product, any service, any city and any 
            country. In essence, a good branding position combines three 
            separate elements:<br>
            <br>
            1. A good branding position differentiates your brand from the 
            competition. BMW, for example, has positioned itself as the 
            ‘ultimate driving machine’. No other automobile brand is associated 
            with the attribute of ‘driving’.<br>
            <br>
            2. A good branding position motivates consumers to prefer your 
            brand. Why would consumers want to buy an ‘ultimate driving 
            machine?’ Because the position implies that a BMW is fun to drive.
            <br>
            <br>
            3. A good branding position has a singular focus. It doesn’t appeal 
            to everyone. Does ‘fun to drive’ appeal to everyone? No, it doesn’t. 
            Some people want an inexpensive car. Some people want a prestigious 
            car. Some people want a safe car. And some people want a car that’s 
            fun to drive. As a matter of fact, any position that appeals to 
            everyone is bound to be useless in building a brand.<br>
            <br>
            With these principles in mind, let’s look at how some cities and 
            countries could brand themselves.<br>
            <br>
            <b>What’s Singapore?</b><br>
            Singapore, for example, is spending millions on a marketing campaign 
            called ‘Uniquely Singapore’. <br>
            1. Does ‘uniquely’ differentiate Singapore from every other country 
            in the world? Not at all. Oman is unique. Every city or country is 
            unique in some way.<br>
            2. Does ‘uniquely’ motivate consumers to prefer the brand? Not in my 
            mind. I like Paris because it’s Paris. Rome because it’s Rome. 
            London because it’s London. They are all unique, but they also stand 
            for something in the consumer’s mind.<br>
            3. Does ‘uniquely’ appeal to everyone? As a matter of fact, it does. 
            That’s why a slogan like ‘Uniquely Singapore’ is easy to sell to the 
            powers-that-be and almost useless as a branding concept.<br>
            <br>
            How do you develop a branding programme for a city-state like 
            Singapore? You look for ways to differentiate Singapore from all 
            other cities in the world. Actually, that’s not hard to do. 
            Singapore is a city of high-rise apartment towers. <br>
            <br>
            There are very few private homes, which is not surprising because 
            the population density is 18,482 per square mile. Of the 194 
            countries in the world, Singapore is second only to Monaco in 
            population density. (By way of comparison, Oman has 33 people per 
            square mile. America has 79. The most crowded country in the Middle 
            East is Bahrain, but it has only 2,746 people per square mile.)<br>
            <br>
            It happens that two of the most pressing problems in the world today 
            are the rising consumption of energy and the associated pollution 
            the energy consumption produces. Singapore is a winner on both 
            counts. High-rise living greatly reduces both per-capita consumption 
            of energy and production of pollutants. <br>
            <br>
            As the world’s population continues to climb, these problems are 
            only going to get worse. What is Singapore? It’s the City of the 
            Future. The ‘City of the Future’ idea also works well with many 
            other aspects of Singapore, which is one of the cleanest, safest 
            cities in the world. <br>
            <br>
            <img border="1" src="cover1a.jpg" width="200" height="140" align="right" hspace="4" vspace="1"><b>What’s Sydney?</b><br>
            Shortly before the September 2000 Olympics, I met with tourist 
            promotion people in Sydney, Australia. The eyes of the world will be 
            on you, I pointed out. This is the perfect time to launch a 
            marketing programme to promote Sydney.<br>
            <br>
            What’s Sydney? If you could answer that question in a single word or 
            concept, you could drive the idea into the minds of the millions of 
            people who will be watching the Olympics, as well as the thousands 
            of journalists who will be covering the games. (Cities need a 
            position separate from the country they are located in.) Paris is 
            the ‘City of Light’. New York is the ‘Big Apple’. Rome is the 
            ‘Eternal City’. But what is Sydney? I outlined four criteria for a 
            position for Sydney.<br>
            <br>
            1. It should be a concept that would position Sydney as a ‘world 
            class’ city along with London, Paris, Rome, New York and Hong Kong.<br>
            2. It should be a concept that has a strong element of 
            believability. People who know Sydney should hear the theme and say, 
            “Yes, Sydney is like that.”<br>
            3. It should be a concept that is alliterative with the name Sydney. 
            That way the memorability of the idea would be enhanced.<br>
            4. It should be a concept that is consistent with the symbol of the 
            city, the Sydney Opera House, one of the five most-recognized 
            buildings in the world.<br>
            <br>
            There is only one idea that fits these four criteria. It’s a simple 
            idea and also obvious. And with the help of the Olympics it could 
            have been put in the minds of millions at a very low cost. <br>
            <br>
            “Sydney, the world’s most sophisticated city.”<br>
            <br>
            If you’ve been to Sydney, you know that this theme rings true. Your 
            one objection might be that Australia with its Outback and Crocodile 
            Dundee images don’t support a sophisticated position. True. But New 
            York City is not America. And Sydney is not Australia. Sydney is a 
            separate brand and needs a position totally different from the 
            position of Australia.<br>
            <br>
            On a map, Sydney is inside Australia. But inside a mind, ‘Sydney’ 
            and ‘Australia’ are in two different places. Sydney is 
            sophisticated. Australia is not. New York is sophisticated. America 
            is not.<br>
            <br>
            <b>What’s Panama?</b><br>
            Panama, a country in Central America, is best known for its canal, 
            yet the country itself is rather poor with a per-capita 
            gross-domestic-product of only $7,300. How should Panama position 
            itself?<br>
            <br>
            My thought is that Panama should be the world’s first ‘free-trade 
            country’. Thanks to the Panama Canal, the country is an ideal 
            assembly and distribution point for shipping products around the 
            world. Although Panama does have free-trade zones, its import 
            tariffs are among the highest in Latin America, ranging from 3 to 50 
            percent (compared with Mexico’s 5 to 20 percent.)<br>
            <br>
            But aren’t tariffs a political problem? Sure, but you can’t isolate 
            marketing programs from politics. I have seldom worked for a client 
            on a marketing project without suggesting changes. Some minor, 
            mostly major. And the future success of the brand was more directly 
            related to those strategic changes. That was all the tactical help I 
            could provide.<br>
            <br>
            <b>What’s Guatemala?</b><br>
            What do most people know about the country of Guatemala? Not much, 
            except it’s a poor country in Central America. That’s not much of a 
            reason to go there.<br>
            <br>
            Actually, Guatemala is a country rich in heritage. It was the 
            cultural center of the Mayas, the most advanced civilization in all 
            of North and South America before the arrival of the Spanish. Even 
            today, 44 percent of Guatemala’s population of 13 million people are 
            of Mayan descent. Many still speak dialects of the Mayan language.<br>
            <br>
            With mountain ranges as high as 10,000 feet and a culture seemingly 
            unchanged for 500 years, Guatemala is a tourist paradise. Scattered 
            throughout Guatemala are hundreds of spectacular Mayan ruins. 
            Cities, temples, houses, playing fields, the relics of a glorious 
            past.<br>
            <br>
            Guatemala has everything a world-class tourist destination could 
            want…except tourists. Very few people know or care about the 
            country. A marketing programme focused on Mayan culture could draw 
            many tourists to Guatemala. There’s one problem, however. Even 
            though Guatemala was the center of Mayan civilization, there are 
            also Mayan ruins in Belize, El Salvador, western Honduras and 
            southern Mexico. <br>
            <br>
            Furthermore, how do you solve the confusion problem? In addition to 
            Guatemala, Belize, El Salvador and Honduras, Central America is home 
            to three other countries: Costa Rica, Nicaragua and Panama.<br>
            <br>
            How do you solve the confusion problem? You change the name of the 
            country from Guatemala to Guatemaya. This change solves both 
            problems. It pre-empts the Mayan position and it serves as a memory 
            device to link the Mayas to the county which contains the most 
            spectacular Mayan artefacts.<br>
            <br>
            My Guatemaya idea was well received among the business community in 
            Guatemala City. They felt it would also solve a political problem in 
            the country. (The Mayan-speaking people feel isolated from the 
            Spanish-speaking majority.) Will it happen? Probably not. Change is 
            very difficult to achieve.<br>
            <br>
            <b>What’s Peru?</b><br>
            Another country with a tourist problem is Peru. This South American 
            country with a population of 27 million draws only 400,000 tourists 
            a year. This is odd because Peru is the home of Machu Picchu, which 
            along with the Taj Mahal, the Eiffel Tower and the Egyptian Pyramids 
            are four of the world’s most famous tourist destinations. Strange as 
            it may seem, Peru needs to move away from its single-minded 
            promotion of Machu Picchu to the promotion of the country as a 
            whole.<br>
            <br>
            If the only tourist attraction in France were the Eiffel Tower, how 
            many tourists would the country attract? Not very many. As 
            attractive as the Eiffel Tower is, it’s not enough to justify a trip 
            to France. The same is true of Machu Picchu. It’s a spectacular 
            site, but not enough to justify the trip to Peru. <br>
            <br>
            On the other hand, a country needs a single focus if it is going to 
            attract tourists. The country of France has a single focus, Paris. 
            When you get to Paris, there are a lot of places to visit including 
            the Eiffel Tower.<br>
            <br>
            Where is the Peruvian analogy? Where is the Paris of Peru? We think 
            it’s the city of Cusco. When you get to Cusco, there are a lot of 
            places to visit including Machu Picchu. Could Peru market Cusco as 
            the ‘Paris of Peru?’ Absolutely not. It’s a terrible name (sounds 
            like an Italian dessert or worse) and has very little recognition 
            around the world. Furthermore, the name ‘Cusco’ does not connote the 
            important historical background of the city.<br>
            <br>
            What’s the real significance of the city of Cusco? Cusco was the 
            center of Inca culture. It’s the home of the Incas, using the ‘king’ 
            meaning of the word Inca, rather than its ethnic meaning. What Peru 
            needs to do is to change the name of the city to reflect its true 
            heritage, the home of the Incas. My suggestion: “Ciudad de las 
            Incas.” (City of the Incas.) When you get to Ciudad de las Incas 
            there are many places to visit including day trips to spectacular 
            ruins like Machu Picchu. <br>
            <br>
