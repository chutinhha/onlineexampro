<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Marine Protection</b><br>
The environmental protections provided in the Environment Law are not limited to 
only the land in Oman. The Environment Law provides that no ship may discharge 
oil or any oil mixture or any other environmental pollutants in the internal 
waters or territorial waters of Oman. <br>
<br>
Furthermore, the Owner, captain or other person responsible for any marine 
structure, and those responsible for the transportation of oil, gas, and 
environmental pollutants within the territorial waters of Oman, as well as 
institutions licensed to explore, extract or exploit oil, gas or any hazardous 
substances (whether in water or land) must immediately notify the Ministry of 
any oil or pollutant spill incident. Such notification shall specify the 
circumstances of the incident, the type and quantity of spilled substance, and 
the remedial measures taken to stop or control the spillage.<br>
<br>
<b>Keep it clean</b><br>
Although the variety of penalties that may be imposed in accordance with the 
Environment Law are too numerous to cover in this article, such penalties range 
from a fine of up to RO1 million, to a sentence of life imprisonment for the 
most egregious offences. As evidenced by the provision of the Environment Law, 
Oman takes the preservation of its environment seriously, and expects businesses 
and individuals to do the same. <br>
