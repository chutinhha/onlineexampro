<br>
<b>Protective Measures</b><br>
Although Oman has had an environmental law in place since 1982, it has been 
replaced by Sultani Decree 114/2001 promulgating the Law on Conservation of the 
Environment and Prevention of Pollution (the &quot;Environment Law&quot;), which is now 
the primary legislation that governs the preservation and protection of the 
environment in Oman. The Environment Law defines &quot;environment&quot; as the setting in 
which human beings live, including living organisms such as men, animals and 
plants as well as the surrounding air, water, soil and solid, liquid, gaseous or 
radioactive substances in addition to man-made stationary or non-stationary 
establishments. This definition is intentionally broad in order that the 
provisions of the Environment Law will be applicable to almost any aspect of the 
environment that may be contemplated. <br>
<br>
The provisions of the law are unequivocal with regard to protecting the 
environment. The Environment Law strictly prohibits the use of the Omani 
environment for the purposes of disposing of environmental pollutants in such 
quantities and types as may adversely affect its condition and its natural 
resources, natural conservation areas, and the historical and cultural heritage 
of the Sultanate. Furthermore, no pollutants may be disposed off in any natural 
ecosystem, unless such disposal is in accordance with the regulations and 
conditions issued by a decision from the Minister of Regional Municipalities, 
Environment and Water Resources (the &quot;Minister&quot;).<br>
<br>
Regarding artificial structures, the Environment Law provides that no structures 
or work sites may be established before obtaining an environmental permit 
confirming the environmental soundness of such structures and sites. In order to 
obtain such a permit, the person responsible for the land (the &quot;Owner&quot;) must 
submit an application to the Ministry of Regional Municipalities, Environment, 
and Water Resources. The Minister will then issue a decision specifying the 
procedures, conditions, and rules regulating the conditions, term and renewal of 
such a permit.<br>
<br>
The Environment Law also imposes a statutory duty on the Owner of the land to 
take any necessary measures, and to adopt state-of-the-art techniques that are 
approved by the Ministry in coordination with the concerned authorities, in 
order to minimise the amount of waste generated at the site. The use of clean 
production techniques to prevent any pollution of the environment and to protect 
its natural resources is also required. Furthermore, the Owner is required to 
submit a pollution contingency plan for approval by the Ministry. This plan is 
reviewed periodically.<br>
<br>
However, the Environment Law recognises that in some cases the production of 
waste and pollution is unavoidable. In this regard, the Environment Law provides 
that the Owner is prohibited from discharging, ordering the discharge, or 
allowing the discharge of any environmental pollutants at the site that is under 
his supervision and responsibility, unless the levels of such pollutants are 
equal to or less than the percentage pollutant discharge specifications provided 
in the implementing regulations and decisions relating to the Environment Law, 
or equal to or less than the percentage that may be specified in the Owner's 
environmental permit. <br>
<br>
These pollutant discharge levels may be exceeded to ensure the safety of the 
site or to save lives in cases of emergency due to an unanticipated fault in the 
operations or equipment at the site, provided that the Owner takes immediate 
remedial measures, informs the Ministry of the situation, and complies with any 
decision made by an environmental inspector.<br>
<br>
It is worth noting that the Environment Law reiterates in several articles that 
the Owner must immediately inform the Ministry in writing of any pollutant 
discharge that contravenes the Environment Law, its implementing regulations and 
decisions, or the terms of the environmental permit issued to the Owner. In such 
instances, the Owner must specify the reasons for and nature of the incident or 
the excess pollutant discharge, together with the remedial measures taken and 
the time required to rectify the situation. The Owner is also required to keep 
records detailing the quantities, types and source of pollutant discharge. The 
Ministry has the right to examine these records at any time.<br>
<br>
Those of us who enjoy wadi trips will be pleased to note that the Environment 
Law prohibits the discharge of hazardous waste or substances, or other 
environmental pollutants in wadis, watercourses, groundwater recharge areas, 
rainwater and flood drainage systems, or aflaj and their channels.</font></p>
