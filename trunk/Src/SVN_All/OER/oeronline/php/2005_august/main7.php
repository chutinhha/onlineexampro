            <span style="font-size: 16pt; font-weight: 700"><br>
</span></font><b><font face="Verdana" color="#ff0000" size="5">Turbo power</font></b><font face="Verdana" size="1"><b><br>
            <br>
            Great ergonomics and superb styling make the Audi A4 Turbo one of 
the hottest sedans in town, reviews Anne Kurian</b></font></p>
            <p align="center" dir="ltr"><font face="Verdana" size="1"><b>
            <img border="0" src="car.jpg" width="250" height="125" hspace="5" vspace="1"></b></font></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="4">A</font><font size="1" face="Verdana">t 
            first glance, the impression you get of the new Audi A4 Turbo is 
            that of an elegant, compact and luxurious automotive. But get behind 
            the wheels and you’ll know that there is much more to it than the 
            looks. The 1.8-litre four-cylinder engine revs to life in a jiffy 
            and delivers what it promises. <br>
            <br>
            Acceleration is not limited to the car; it is for your pulse too! 
            The new Audi A4 Turbo takes to the road with ease, providing immense 
            driving pleasure with amazing torque factor. <br>
            <br>
            The A4 Turbo is an apt example of Audi’s maxim Vorsprung durch 
            Technik, which means ‘Advancement through Technology’. Any doubts if 
            the A4 Turbo could measure up to its siblings A8 and A6 are cleared 
            after a spin in this new sedan. The A4 Turbo has its share of 
            appealing features that make the sedan a potential winner in the 
            long run.<br>
            <br>
            Apart from the winning features, which we’ll discuss later, the A4 
            Turbo’s powerful suspension handling elevates the driving pleasure 
            and even prompts you to push the limits, but without overdoing it. 
            For, an Audi is an Audi and the A4 Turbo is a definite example of 
            the decades old technological prowess of the car manufacturing giant 
            when it comes to performance, safety and satisfaction. <br>
            <br>
            The exterior story<br>
            The A4 Turbo is sleek yet there’s a brawny feel to the body, which 
            is one of the first aspects that you notice about the otherwise 
            compact sedan. The exterior body styling, which includes an imposing 
            ‘nose’, adds visual strength to the A4 Turbo. The headlights angle 
            upwards and wrap around the fenders, a feature that gives the sedan 
            an aggressive appearance. An impressive and welcome feature of the 
            A4 Turbo is the headlight cleaning system that uses high-pressure 
            water when the headlights get fogged or dirty and are placed just 
            below the headlights. So you can be sure of high-power beam all 
            through your drive at any point. As for the high beam, Audi does not 
            disappoint when it comes to illumination. The Xenon plus headlights 
            are amazingly powerful. <br>
            <br>
            The other features included in the A4 Turbo are gas struts for the 
            bonnet, fog lights, heat-insulating glass, electrically adjustable 
            exterior mirrors, tinted side and rear windows, automatic boot lid 
            and dual exhaust tailpipes. And while reversing, any worries of 
            backing into a wall or another car can be put aside, thanks to the 
            acoustic parking system. <br>
            <br>
            The interior story<br>
            The sedan is quite a compact one and the interiors are as compact as 
            they can get. There’s not much wastage or taking of space in the 
            design ergonomics. The craftsmanship is impeccable and not over the 
            top. The feel of the interiors of the A4 Turbo is luxurious when it 
            comes to the leather seats, wooden trimmings and design. As soon as 
            you seat yourself behind the steering wheel, the comforting feeling 
            of being in control of the car is unmistakable. The controls in the 
            A4 Turbo are designed to be within easy reach of the driver and what 
            caught our eyes was the clutter-free look of the instrument panel - 
            electronic speedometer, rev counter, coolant temperature display, 
            fuel gauge and driver information system. <br>
            <br>
            The leg space inside the A4 Turbo is not as expected from an Audi. 
            In fact, the front passenger may find the fire extinguisher placed 
            on the left side of the floor a wee bit uncomfortable. Audi’s 
            concern for safety is commendable, but placing the extinguisher 
            below the seat would have been more welcome. <br>
            <br>
            Moving on to the music system, it’s impressive. The A4 Turbo boasts 
            of both a stereo system and CD player; the latter is smartly stashed 
            away in the glove compartment and yet takes very little space. 
            Another impressive feature of the A4 Turbo is the powerful air 
            conditioning system that includes separate controls for the driver 
            and front passenger. <br>
            <br>
            Storage space needs a mention too. Two-cup holder next to the 
            handbrake, front door side pockets, the center console that two 
            level storage facility and also doubles up as armrest, and rear 
            center console cum armrest that can hold cups, knick knacks and also 
            comes with a first aid kit. <br>
            <br>
            Yet another feature that caught out attention was the automatic sun 
            blind for the rear glass, which is handy in this scorching heat. <br>
            <br>
            Performance prowess and safety factors<br>
            On the roads, the A4 Turbo does not fail to impress. Audi’s famed 
            engine output can be felt in this new sedan too within moments of 
            getting the A4 Turbo on the road. The pick-up is fast, even after 
            braking and driving. The new Audi not only has a sporty feel in the 
            looks department but also when it comes to performance. The urge to 
            increase the momentum is irresistible and if it weren’t for the 
            radars, hitting 180 would be a temptation that you just might not be 
            able to repress. We had a hard time trying to maintain the speed 
            limit, though the Qantab stretch satiated much of our speed love.
            <br>
            <br>
            Controlling the A4 Turbo is comfortable and as a result, takes your 
            driving experience notches up the performance ladder. The sedan 
            glides effortlessly even at sharp, unexpected curves and the 
            steering response is brisk. <br>
            <br>
            And as far as safety is concerned, Audi leaves nothing to chance. 
            The safety package includes crash-optimised deformation zones, 
            driver and front passenger full-size airbags, three-point safety 
            belts for all seats and active head restraints in the front. Even at 
            high-speed, the brakes function exceptionally well, making it 
            possible to bring the car to a sudden halt.<br>
            <br>
            To sum it all up, the A4 Turbo is a sedan that combines superb 
            ergonomics with striking styling that makes journeys, short or long, 
            an enjoyable experience. Undoubtedly another triumphant creation 
            from Audi!</font><br>
