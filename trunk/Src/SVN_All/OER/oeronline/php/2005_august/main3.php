</font><b><font face="Verdana" size="1"><br>
</font><font face="Verdana" color="#FF0000" style="font-size: 18pt">Korean and 
Chinese brands are hot</font><font face="Verdana" size="1"><i><br>
<br>
</i>The courage of conviction has made the Korean and Chinese brands stand up 
and challenge the monopoly of traditional global leaders, <i>OER</i> analyses.</font></b></p>
<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>K</b></font><font face="Verdana" size="1">orean 
businessmen have silenced their critics. When they set out 20 years ago to join 
the stable of world-class brand companies, no one could have predicted that 
Samsung, LG, Kia and Hyundai would be the up-and-coming global brands they are 
now. Their success story is truly amazing. 
</font></p>
<p align="center" dir="ltr">
<img border="0" src="kia.jpg" width="350" height="130"></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">“In the past, there might have been a quality perception generally associated 
with Korean-made brands, but it no longer holds true. Gone are the days when 
buying a Korean product was considered a compromise. A quantum leap in quality, 
style and value-for-money has put Korean automotive brands right there in 
peoples’ consideration set,” says a Reliable International Automotive, the 
distributors for Kia automobiles in Oman, spokesperson. <br>
<br>
Like the Koreans, Chinese brands too are raring to ‘go global’. Being the 
world’s low-cost factory floor is no longer China’s singular ambition, analysts 
say. Today of course, it’s the Koreans who are emerging as top brands globally 
and in Oman. The study of the top 100 global brands by Businessweek-Interbrand 
has ranked Samsung as the world’s 20th largest brand. In Middle East and Africa 
(MEA) region alone, it is targeting revenue of $3.5 billion for the year 2005. 
Maintaining its year-on-year growth of 30 per cent, Samsung has reported a 
remarkable performance in 2004, with annual sales revenues in the MEA region 
crossing US$2.7 billion. According to Syed Aftab Ahmed, commercial manager of 
SARCO (the dealers of Samsung electronics and home appliances), “Samsung is the 
fastest growing global brand. It has increased its sales tremendously in Oman at 
an average of 30 per cent over the last three years.” <br>
<br>
Close on its heels is LG, today reckoned as a global force in electronics, 
information and communication products with its 2004 annual sales at $38 billion 
(consolidated). “LG has been in Oman for the last 10 years. LG has been a 
consistent number one player in three product categories - air conditioners, 
refrigerators and washing machines - for the last six years in a row in terms of 
volumes. In the brown goods segments, we are among the top three players. We are 
growing at a very fast pace,” says Shaji Joy, Manager Digital Products - 
Electronics &amp; Home Appliances Division of Oman Gulf Enterprises (OTE Group)which 
represents LG in Oman exclusively. <br>
<br>
<img border="1" src="br1.jpg" align="right" vspace="1">Hyundai too is emerging as a fierce competitor in the global auto market. In 
1998, Hyundai was ranked 12th among global manufacturers but it has set the goal 
to be among the top five automakers in the world by 2010. This Korean brand has 
been received quite well in Oman also. Mithilesh Kumar, General Manager, OTE 
Group says: “In Oman, the performance of Hyundai cars has been exceptional both 
in terms of volumes and quality. After the JD Power Associates Quality Study, 
the volumes have been increasing very rapidly. Today, Hyundai offers a car for 
every segment of car buyer in Oman through its ever increasing model line up.”<br>
<br>
Kia Motors Corporation, founded in 1944, is Korea’s oldest manufacturer of motor 
vehicles. Kia aims to become one of the world’s premier automotive brands. Kia’s 
overseas retail sales of passenger cars and light trucks in June 2005 stood at 
79,116 units, representing a 23.2 per cent year-on-year increase and an all-time 
monthly sales record. <br>
<br>
While ‘Made in China’ products have flooded international markets, including 
Oman, the Chinese brands are just beginning to make their presence felt. Haier 
Group, a $10 billion maker of refrigerators, washing machines and other electric 
household appliances leads the way and nurtures global ambitions. Other ‘Made in 
China’ goods like garments, toys, novelties, stationery and household items, 
which do not have a brand appeal, are in high demand in Oman and in the 
Middle-East, says a spokesperson of a leading local supermarket who are direct 
purchasers from China. “The segment of consumers that looks for durable products 
with a reasonable price tag, goes for these non-branded goods. The Chinese give 
importance to packaging and this is an important factor on which they score over 
their Asian neighbours,” he explains. <br>
What makes Korean and Chinese brands successful?<br>
<br>
<b><img border="1" src="br2.jpg" align="left">Investment in R&amp;D </b><br>
With plenty of cash in hand, Samsung has made R&amp;D a top priority. “It is the 
only electronics company that spends more than 30 per cent of its profit on 
R&amp;D,” says Syed Aftab Ahmed. Rather than outsourcing of manufacturing, the 
company sinks billions of dollars into huge new factories. Instead of focusing 
on few core competencies, Samsung remains diversified and vertically integrated. 
Samsungs chips and displays are used in the making of its own digital products. 
“If we get out of manufacturing,” says Samsung CEO and Vice Chairman, Yun Jong 
Yong, “we will loose.”<br>
<br>
Investing in R&amp;D has obviously paid good dividends to Samsung. Today, it 
produces tech-savvy products for its trend-conscious customers. Last year, it 
won five industrial excellence awards for its digital appliances. “Innovative 
designs, features and superior products like the E530 mobile phone for women, 
E730 as an entertainment phone and D600 with TV output give Samsung a cutting 
edge in the competitive telecommunications market,” says Sanjay Saxena, General 
Manager, Mustafa Sultan Telecommunications. “Also lined up in the second half of 
2005 are technology-first phones. Our phones are designed to suit local customer needs and across cultures and we have a proactive distribution,” he adds. <br>
<br>
Similarly, LG pursues growth strategies involving ‘fast innovation’ to secure a 
high competitive edge. Hee Gook Lee, CTO, LG Electronics informs: “LG 
Electronics’ strategy for R&amp;D is to map out new standards in newly emerging 
areas by developing technologies that link the Internet and network while 
maintaining efforts to introduce new products that are global leaders. LG 
Electronics has already introduced products with world-class technology in 
digital TV, PDP - the next generation display, optical storage and CDMA. We’ll 
also continue to offer our customers the digital consumer electronics that were 
once only in the realm of dreams, including refrigerators, washing machines, and 
air conditioners that can access and surf the Internet.” <br>
<br>
Kia too has major focus on R&amp;D. The company maintains four overseas R&amp;D centers, 
two in the United States, one in Germany and one in Japan. Currently, Kia spends 
about 7 per cent of its revenue on R&amp;D.<br>
<br>
<img border="1" src="br3.jpg" align="right"><b>Production Speed</b><br>
In order to seriously compete and keep abreast with international brands, it is 
important to move fast. Samsung CEO Yun and his boss, Samsung Group Chairman, 
Lee Kun Hee, firmly believe that the electronics industry’s shift from analog to 
digital, making many technologies accessible, would leave industry leadership up 
for grabs. In the digital era, Yun says, “If you are two months late, you are 
dead. So, speed and intelligence are what matter and the winners haven’t yet 
been determined.” The ingredient is speed. Samsung now takes an average of five 
months to go from new product concept to rollout compared to 14 months six years 
ago.<br>
<br>
Kia has also made sure that its production keeps pace with its high growth. Kia 
ended the year with annual sales figures up by 70 per cent in Europe, 27 per 
cent in North America and 21 per cent in China. Kia Motors’ Senior Executive 
Vice President and COO, Yong-Hwan Kim says: “This past year was full of many 
significant events for us at Kia Motors. We broke ground on our first European 
plant in Slovakia, released three successful models, and have firmly established 
ourselves as the fastest growing car makers in the world.” Kia currently has 
three production centers in Korea and one overseas plant in China. Most 
recently, Kia has begun construction on two new overseas plants. The first plant 
in Slovakia will be capable of producing 300,000 units annually. The second 
plant will be located in China next to the existing Jiangsu facility and the two 
facilities combined will be able to produce 430,000 units every year.<br>
<br>
In production speed, China is preferred to its Asian neighbours, making it the 
world’s biggest manufacturer of unbranded goods. What actually pulls buyers to 
China is the speed at which new product varieties are made. “There are always 
new things to see in the market. You just turn your back at a shop and a new 
item is placed on the shelf,” says the spokesperson of a reputed local 
supermarket that also deals in Chinese products.<br>
<br>
<b>High Quality </b><br>
Globally, Kia is scoring on quality with Sorento, Cerato and Carnival, receiving 
high J.D Power ratings. “Kia continues to move ahead with a strong focus on 
enhancing value for every customer. Specific mention can be made here of the 
performance of individual models - of the surge of Picanto and Cerato in the 
passenger car segments, the growth of Carnival in the multi purpose vehicle 
category; the rising popularity of Sportage and Sorento in 4-wheel drive 
segment, and the impressive performance of Opirus with our discerning, yet 
value-conscious customers,” asserts Reliable International Automotive 
spokesperson.<br>
<br>
“In its Initial Quality Survey, J.D. Power and Associates ranked Hyundai’s 
Sonata as the leader in its class and their other models too have scored 
impressively in surveys. The April Consumer Reports found the 2004 Sonata as the 
most reliable car on the road with just two problems per 100 vehicles,” says 
Mithilesh Kumar of OTE Group. He adds, “Hyundai’s ever improving quality has 
helped build brand loyalty among car buyers in Oman. The variants available in 
Hyundai cater to the requirements of every segment of automotive buyers in Oman 
and satisfy customers of any kind, base to luxury car buyers. The pricing of our 
models has also contributed a lot for increase in volumes. We offer very good 
value for money. <br>
<br>
LG has been consistently awarded for its high quality internationally with many 
awards such as IF Design Award, the Reddot Design Award and the Ergonomic Design 
Award. Making the best use of technology to make high quality consumer products, 
LG Electronics is also leading the international expansion of groundbreaking 
&quot;Home Network&quot; technology, connecting all home appliances - wired and wireless. 
Joy adds, “Today everybody connects LG with high quality and latest technology 
products. Speaking about our quality, LG is the first company in the world to 
launch a 71-inch plasma display (the largest in the world) commercially 
available. It comes with 24-carat gold plating, catering to a very niche target 
group. We are launching this in Oman in August. We are also launching the blue 
tooth mobile phone in Oman soon which again speaks of our high quality line-up.”<br>
<br>
<b>Brand Building &amp; Market Segmentation</b><br>
The Korean brands are very aggressive on the strategic brand-building front. 
They use their global promotion strategy with regional flavour to penetrate the 
market. Take Kia for example. “To enhance customer delight, currently, we have 
launched the Summer Campaign, with many benefits on offer. It has received 
enthusiastic response. This, too, has added to the satisfaction levels of our 
customers. We listen to what the customer is saying. Thereafter, it is a matter 
of fulfilling the customers’ needs and wants in the best manner possible. We 
believe in surpassing customer expectations and creating value. This is what our 
brand theme ‘The Power to Surprise’ signifies. This is what we live up to. And 
at the end of the day, this is our cutting edge,” asserts Reliable International 
Automotive spokesperson.<br>
<br>
Samsung Electronics has currently positioned itself into four main business 
units, Home Network, Office Network, Mobile Network, and Core Components. 
Currently, Samsung Electronics is the world’s largest computer display 
manufacturer. The product line up in the TV’s are Flat TV, Projection TV, DLP, 
(Digital Light Processing), PDP (Plasma Display Panel) and LCD (Liquid Crystal 
Display). In the A/V, they have DVD players, DVD Combo, DVD Recorders, Home 
Theater system, Mini Hi Fi system, Macro Hi Fi system, DVC and MP3 players. Home 
appliances include washing machines, refrigerators, air conditioners, microwave 
and vacuum cleaners etc.<br>
<br>
For 2005, Samsung has lined up a host of technologically advanced, 
first-of-its-kind digital products such as feature-powered PDAs, mobile phones, 
sophisticated software-enabled monitors, extensive line-up of flat-panel 
displays comprising LCDs and Plasma TVs, minimalist-design multifunction 
printers and sleek digital devices such as MP3 players and Digital Video 
Camcorders. Their new launches in the Middle East for 2005 include ‘Fast 
Response Time’ Monitors, a new line up of LCD TVs and 10 new digital audio 
players.<br>
<br>
One of the key products that Samsung expects to lead in the Middle East market 
will be its highly-acclaimed PDA (Personal Digital Assistant) phone, SGH-i700, 
which is scheduled to be launched by mid-2005. The phone was showcased along 
with a range of advanced mobile handsets and state-of-the-art digital products 
at the company’s ‘digital convergence’ theme-inspired pavilion in GITEX last 
year. <br>
<br>
Samsung’s current mobile phone market share stands at 20 per cent in Middle East 
and Africa and at 30 per cent in UAE. Furthermore, the company has initiated 
discussions with concerned authorities for 3G handsets following the rollout of 
3G services across several countries in the region. <br>
<br>
LG has also been very aggressive on the brand-building front. Says Joy: “LG is 
one of the largest spenders in any market in terms of brand building. Today 
everybody understands LG as a brand and realises that it stands for the latest 
technology at affordable prices. In Oman, we do promotions on a constant basis. 
Besides the traditional media like print, outdoor etc., we gets associated with 
different kinds of sports and entertainment events through sponsorships.”<br>
<br>
Haier, when it was incorporated in 1984, produced only household refrigerators. 
Today it manufactures a wide range of household electrical appliances, including 
15,100 varieties of items in 96 product lines and exports products to over 100 
countries.<br>
<br>
<b>Competing with international brands</b><br>
The meteoric rise of Korean brands in the international market has certainly 
made their competitors wary. Samsung’s success in digital gadgets and chips has 
wowed consumers and scared rivals around the world. As it muscles its way to the 
top of consumer-brand awareness surveys, Samsung thinks the moment is fast 
arriving when it can unseat Sony Corp. as the most valuable electronics brand 
and the most important shaper of digital trends. Its rivals are taking the 
challenge seriously. “I ask for a report on what Samsung is doing every week,” 
reveals Sony’s president Kunitake Ando.<br>
<br>
Last year, Hyundai’s cars seriously competed with Toyota and Honda in J. D. 
Power &amp; Associates’ annual study of initial vehicle quality. “It is a company we 
must watch out for,” says Honda chief executive Takeo Fukui. Even Toyota, widely 
considered the world’s best automaker, is looking over its shoulder at the 
Koreans. “We’ll watch them more carefully than in the past and try not to be 
overwhelmed by them,” asserts Toyota president Fujio Cho.<br>
<br>
The Korean companies like Kia, Hyundai, Samsung and LG have moved from being 
just a national brand to major global brands quite successfully. But it took 
them years to reach to this level. Analysts say Chinese companies do not have 
that luxury because the rapid pace of globalization means that markets are 
quickly won and lost. <br>
<br>
Like the Koreans, the Chinese are going to stumble many a time as they build 
their brands. Korean brands that are market leaders now also faced skepticism 
when they first appeared in international markets. Counted on the growth rate of 
20 to 30 per cent annually, Chinese brands need at least another three to four 
years to challenge the world's top 500 on the global stage. The biggest leverage 
for Chinese brands and ‘Made in China’ goods in Oman and in the GCC is good 
pricing and good quality products. But it is the staying power of a brand that 
finally determines its global status. <br>
<br>
Kia, Hyundai, LG and Samsung scored well on all counts and are challenging 
international global giants. China’s rise is also inevitable as Chinese products 
are rapidly making inroads in the markets across the globe.
