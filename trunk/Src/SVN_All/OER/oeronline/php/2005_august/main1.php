<font color="#FF0000">Moving beyond Oman's shores</font></font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><br>
<font face="Verdana" size="1"><b>OER gets face2face with Mike Wilson, the 
managing director of Oman Oil Marketing Company SAOG, on omanoil’s new marketing 
strategy and business expansion plans<br>
</b>
<br>
</font><font face="Verdana" size="3"><b>
<img border="1" src="face_t_face1.jpg" width="120" height="99" align="left" hspace="4" vspace="1">A</b></font><font face="Verdana" size="1"><b>s 
part of your new marketing strategy, you have appointed Ali Al Habsi as the 
brand ambassador of omanoil. What is the brand fit between omanoil and Ali?</b><br>
Let’s be candid. People like heroes, success stories, good-looking people; and 
Ali is all of them. He has an amazing fan following. He is recognized by his 
hairstyle alone with kids getting the Al Habsi haircut. Therefore, the 
association with him automatically improves our image also. <br>
<br>
It is a question of what our company stands for. Omanoil is young, has a passion 
for energy, and is passionate about youth. And this is exactly what Ali is. 
Genuinely speaking, you could be seeing the beginning of a top footballer coming 
out of Oman. And we would like to be part of it. <br>
<br>
Look at the average age in Oman. It’s below or about 14 years I think. About 
50-60 per cent of the population belongs to this age group. Every young child 
and man in this country wants to be like Ali Habsi. Now, if they have an 
association with Ali, then with Ali as our brand ambassador, they get associated 
with us also. Therefore, it’s a good fit for both for us. We were looking for 
someone who has the appeal and the mind-set to complement omanoil. And we found 
our match in Ali. <br>
<br>
<b>… but you don’t have a long term deal with him?</b><br>
We’ve been lucky that we have a three-month window with him. Once he signs 
completely with Bolton Wonderers, I don’t think we can touch him. So during this 
three-month period we would like to use him to push our brand. So he becomes our 
face in our retail outlets, advertising and the social work (like going to the 
disabled society) which we do. <br>
<br>
<b>Does it mean that you can’t use him beyond three months?<br>
</b>No. Let me put it this way. How can we use him for a longer period as he is 
here for just few months? But we will use his face. As I said we don’t know the 
legal implications of his Bolton contract. He may not be allowed to work for 
other people. At the moment, he is in a limbo. If possible, we would like to be 
associated with him for many years. Because the more exposure and publicity he 
gets, the more it helps our brand building. <br>
<br>
<b>With less product differentiation and small size of the market in Oman, is it 
right for a company to use sports marketing to push the brand?</b><br>
Why not? Take the case of Hamed Al Wahibi. He has achieved international fame as 
a rally driver. He appears on Mountain Dew cans and trucks. Why? Omani people 
aspire for him. They need a hero to look up to. <br>
Look at Ali. He is a young; good-looking; very popular among kids, males and 
females; and plays the sport very well. So coming back to the brand association, 
this market is no different from any other market. It’s just that you don’t have 
too many heroes here in Oman.<br>
<br>
<b>So you think corporate sector should come forward and use sports 
personalities for brand building?</b><br>
It’s a matter of finding the right fit. I think, if you can find the right 
personality to complement your brand, it could work here like any other market. 
It also depends on what is your target group. Let’s say if your audience is 
young Omani, then Ali could do the trick for you. <br>
<br>
<b>How is the consumer in Oman different from other markets?</b><br>
I actually believe that consumers in most countries are very similar. One thing 
peculiar about Omani customers is that they are far more accepting than any in 
any other market. In other markets, people would complain strongly, here people 
don’t do that. And if they start giving feedback, let’s say about service, the 
service standards will go up. It’s a very polite society, while others are not 
so polite. Omanis are generally nice people.<br>
<br>
<b>It must be making your task difficult when there is no feedback from the 
customers?</b><br>
The problem is you need to ask them through proper market research. But I’m not 
sure whether we ask enough. Some of the respondents may not like to part with 
their answers. Deep down, they know the answers, but they don’t like to tell 
you. If you ask the right questions in the right way, I think we can get the 
right feedback. <br>
<br>
<b>How has the journey of Oman Oil gone in the last few years?</b><br>
We were BP plc company till two-and-half years ago. So we had to get rid of BP 
and establish a new brand ‘omanoil’, which has its own values, logo and ethos. 
And I think we have done reasonably well. The transition from BP to omanoil has 
been good, but the brand message has to be constantly reinforced. It costs money 
and you have to get the right vehicle to promote it. And there are not many 
vehicles out here except ads and ads by themselves don’t mean anything. <br>
<br>
<b>What about the performance on financial front?</b><br>
We have increased profits year-on-year for the last four years. We have met our 
shareholders’ commitment. We are financially sound and are on the right track.<br>
What are the challenges for the oil industry in Oman?<br>
The challenges are like any other market, particularly a regulated market. The 
prices are regulated and the margins are fixed. So there is constant need to 
concentrate on cost containment and build efficiencies, which sometimes detracts 
us from being innovative. The market is growing reasonably steady but there are 
three players in this market, which is more than enough. <br>
<br>
<b>What’s the way forward for the company?</b><br>
I think Oman Oil will be very solid in Oman. We will continue to produce good 
results and provide shareholders with good return on their investments. And I 
think you may well see Oman Oil in other countries, as we are looking at 
expanding outside Oman either through acquisition or expansion. <br>
<br>
<b>Which overseas markets you are eying?</b><br>
We are looking very closely at markets which are very similar to ours and where 
there is room for expansion and a will for further development. The negotiations 
are going on and some of them are fairly critical at the moment. <br>
<br>
We understand Middle East and we are looking at it very closely. The Middle East 
countries excite us, as many of them are opening their industries, speak Arabic 
and share a similar background. We are not thinking much about Asia and Europe 
initially, as we don’t have a great knowledge of those markets.<br>
<br>
We need to look at markets which we understand. We need to know what’s going on 
in the market before you jump in. The other thing is the competitive business. 
You have multinational oil companies all over the place. And to go and compete 
with them head on doesn’t make sense. So you look at areas, which may not be 
attractive to them, but to us, they are appealing and where we get a maximum 
exposure for our investments. </font></p>
