                    </font><font face="Verdana" size="4"><b>Young and upwardly 
                mobile<br>
                </b></font><b><font face="Verdana" size="1">Two mobile service 
                providers have launched youth services. But is everything as 
                cool as it has been made out to be. Visvas Paul D Karra 
                scratches below the surface to find out more</font></b><font face="Verdana" size="4"><b><br>
                <br>
                    </b></font><font face="Verdana" size="1">On July 7, 2009, 
                    Hamad Al Battashi, a young college-goer from Bausher was crowned 
                    the star of Oman when he was judged the best singer at the 
                    finals of the Fannan Halafoni singing competition. The event, 
                    targeting the youth of Oman, was conceptualised by halafoni, 
                    the latest mobile brand in Oman, and was supported by Hala 
                    and Hi FM radio channels. <br>
                <br>
                Meanwhile, Shababiah, Oman’s first youth-focused mobile brand, 
                also announced that it has sponsored the recently launched and 
                highly acclaimed album ‘Wa’tasimoo’, an Islamic singing project 
                by a group of talented young Omani singers. Shababiah, launched 
                last November by Nawras, was the first mobile brand to offer 
                services for the 16-25 age bracket group. <br>
                <br>
                halafoni and Shababiah point out to the general direction in 
                which the mobile operators are headed in Oman at the moment as 
                they target specific segments of the market. In this case, it is 
                the youth. The developments are not surprising though because 
                the numbers speak for themselves. More than half a million 
                (precisely 600,000) of the Omani population is in the age 
                bracket of 15-25 years.<br>
                <br>
                <br>
                <b>Hip and happening</b><br>
                Realising that there is a whole new hip and happening segment of 
                youth out there, telecom companies in Oman have literally 
                decided to put their money where their mouth is. Switching to 
                the youth mode to tap this part of the demographic profile seems 
                to be natural for the companies because it not only maximises 
                their revenues but also increase loyalty among the subscription 
                base. In May this year, Oman became the first in the Middle East 
                to have four mobile operators, two Class I (Oman Mobile and 
                Nawras); and two Class II (FRiENDi of Connect Arabia and Renna 
                from Majan Telecommunications) license holders. The number of 
                post-paid and pre-paid mobile subscribers of Nawras and Oman 
                Mobile stood at 3.4mn in May 2009. Effectively, the four service 
                providers have a subscriber base range of between three to four 
                million. As competition hots up, providing innovative solutions 
                and targeting specific segments is the only way forward.<br>
                <br>
                Tapping the youth segment by offering exciting offers and 
                value-added services is a strategic decision taken by Nawras and 
                Connect Arabia who have launched Shababiah and Halafoni services 
                respectively. Both these services offer a slew of ‘kool’ things 
                to the youth who are looking at various options to maximise 
                every rial they put in their mobile phones.<br>
                <br>
                <b>‘Cool’ benefits</b><br>
                    Nawras says that its Shababiah subscribers have various benefits 
                    like low call rates; free Rannati (personalised greeting service) 
                    subscription; as well as exclusive mobile news and internet 
                    content and access to community websites like Facebook, Twitter. 
                    Shababiah offers all these elements and more, for a monthly 
                    subscription of a mere 500 Baizas. Shababiah has associated 
                    itself with many community related events like supporting 
                    football games and Omani musical shows. <br>
                <br>
                Providing value-added services by associating with the community 
                is the ultimate aim of Shababiah. Says Eric Kerboriou, Nawras 
                marketing director, “I am a strong believer in communities. A 
                community could be identified by its age, mobile preferences, 
                etc. Finding out the needs of these communities and responding 
                to them makes any service provider successful. For example, 
                Apple’s iPhone has responded to a need of providing personalised 
                software to a customer. That is why it has become a success. 
                With Shababiah, we have done exactly that. We have responded to 
                the need of the youth who are in the age bracket of 16-25.”<br>
                <br>
                The halafoni brand carries the tag line Kool Alatool, meaning 
                ‘always cool’, and has positioned itself as a mobile service 
                that offers a prepaid SIM package with many funky extras for 
                users to download such as ringtones, wallpaper, themes, music 
                tracks and logos. “halafoni is a totally new concept in the 
                Sultanate,” says Eleanor Azar, chief marketing officer of 
                Connect Arabia: the company that owns and operates brands like 
                halafoni and FRiENDi mobile. “Our prepaid SIM card has all of 
                the standard functions you would expect like SMS and 
                international roaming but what makes us different is that our 
                product is centered on the needs and aspirations of the young
                <br>
                and trend setting crowd,” adds Azar.<br>
                <br>
                <b>Innovative and agile</b><br>
                    While Shababiah and Halafoni compete head-to-head, Renna, 
                    the second mobile reseller in Oman, is quietly watching all 
                    the action and making plans of its own. Niklas Nielsen, CEO 
                    of Renna is not unduly worried because of the new services 
                    as he believes that competition will create more awareness 
                    about mobile services and will drive the mobile resellers 
                    and operators to be more innovative and agile. Says Nielsen, 
                    “The fact that we see the launch of new services from our 
                    competitor (Connect Arabia) helps us measure see where each 
                    of the mobile operator is standing. However, our position 
                    is that we are a clear and transparent operator. What you 
                    see is what you get. That is what Renna stands for. And therefore 
                    our brand appeals across demographics. That is where we intend 
                    to build our strength. We have a lot of young people who have 
                    chosen to buy our services because they have peace of mind 
                    without any hidden costs.”<br>
                <br>
                However, the question arises as to how sustainable are these 
                youth services and how many young subscribers are there in Oman 
                who want to use the phone more than just a communicating tool. 
                According to Eric Kerboriou, Shababiah has been designed in line 
                with its motto of putting customers first and therefore the 
                service was launched after discussions with many hundreds of 
                young people to find out what they are looking for from a mobile 
                operator. “If there is a demand and if we see that we can grow 
                our business by satisfying our customers, we have to go there. 
                That is why we have taken great care to deliver what young 
                customers want and will continue to talk with new customers as 
                the mobile community grows, delivering new services in line with 
                their expectations and needs,” says Kerboriou.<br>
                <br>
                But it seems that segmenting the population is not easy. 
                Demographic division is not only based on age, gender and 
                nationality as you can segment people by their buying behaviour 
                and their preferences as well. For example, there are a lot of 
                people who care about getting a mobile service that is easy to 
                get, allows cheap calls and does not have any unpleasant 
                surprises.<br>
                <br>
                <b>Cheap or class?</b><br>
                Nielsen is confident that Renna would continue to attract the 
                youth because of the simplicity of its services. “I think we 
                have a lot of youth in our customer base and we will definitely 
                get more. A lot of the youth find our brand appealing, because 
                of its simplicity and because it is an Omani brand.”<br>
                <br>
                Continuing further, Nielsen says, “I don’t think that all the 
                youth are interested in the new youth services. But I think the 
                majority of the youth are those who are very conscious about 
                their money, who are studying hard in colleges and are in the 
                transition phase, transforming from children to grown-ups; from 
                one social class to another. Studying hard in order to get their 
                grades and becoming successful in life is their priority. And 
                they would not be interested in downloading the hip cool 
                ringtone from MTV or Rotana charts or buying a ticket to a 
                concert, or identifying with the most happening pop star,” 
                Nielsen points out.<br>
                <br>
                    But on the other hand, there is an affluent and emotional 
                    segments in the population of young people who do not care 
                    about how much they spend. Such a segment owns flashy cars, 
                    attend cool shows and is seen in the happening places with 
                    the latest handsets and download hip content. The probability 
                    is that they will choose such a service but their percentage 
                    is a minority.<br>
                <br>
                Telecom experts opine that at the end of the day, whether it is 
                Halafoni or Shababiah, it is all about a business case. You need 
                to cover the operating costs with the revenues you generate. 
                When you are tapping a particular segment, one needs to have a 
                marketing strategy. The second very important building block is 
                to align the operational model after the marketing approach. For 
                example, if you have identified the youth segment, then you 
                define your marketing strategy on how you are going to attract 
                this segment. But if it’s going to cost you an enormous amount 
                of money to do and this segment is a relatively small, then you 
                have failed in your second part of the exercise.<br>
                <br>
                “The challenge before mobile providers is how to make functional 
                features available without incurring too much burden on 
                yourself. Otherwise, you need to make it interesting and 
                attractive enough. That again is the challenge but is also very 
                interesting. It is easy to get exciting about launching a youth 
                service but then you have look at the operational costs, the 
                cost of hiring new people, the cost of marketing the new service 
                etc. All that is doable but it is difficult to sustain,” adds 
                Nielsen.</font><b><font face="Verdana" size="4"><br>
