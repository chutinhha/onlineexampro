                    </font><b><font face="Verdana" size="4">Opportunities Galore 
                    In Austria</font></b><font face="Verdana" size="1"><br>
                    <b>Austria wants to promote bilateral investment opportunities 
                    in various sectors. A trade mission visited Oman to bolster 
                    the business ties</b><br>
                    <br>
                    Austria is keen on attracting Omani investors to capitalise 
                    on the number of business opportunities especially in the 
                    manufacturing sector. On a reciprocal basis, Austrian investors 
                    are also exploring new avenues to do business in the Sultanate. 
                    The visit of a 15-member trade delegation to Oman in March 
                    was significant and was also a great success. The companies 
                    that were part of the trade delegation represented various 
                    sectors like oil and gas, construction and allied industry, 
                    lighting equipments, wood manufacturers, alternative and renewable 
                    energy, software developers etc.<br>
                    According to the trade mission head, Christoph Grabmayr, regional 
                    director, Africa/Middle East, Austrian Federal Economic Chamber 
                    – Austrian Trade, the 15 companies that participated in the 
                    mission gave emphasis to the existing trade relations between 
                    the two countries while paving the way to explore new avenues 
                    for trade and commerce in the Sultanate. The trade delegation 
                    held meetings with officials of the Oman Chamber of Commerce 
                    and Industry (OCCI) as well as with Omani Centre for Investment 
                    Promotion and Export Development (OCIPED).<br>
                    <br>
                    Since the early 90s, the Austrian economy has taken the globalisation 
                    route with exports (mainly goods and services) accounting 
                    for nearly 60 per cent of the Austrian GDP. Around 72 per 
                    cent of the Austrian exports go to the EU member states. At 
                    the same time, Austrian exports to Oman grew significantly 
                    during the past few years. Exports to Oman went up by 14 per 
                    cent in 2006 and 55 per cent in 2007 to reach RO38mn. However, 
                    due to the global economic slowdown, the bilateral trade remained 
                    constant in 2008. According Grabmayr, the fact that many Austrian 
                    companies have already established their presence in Oman 
                    means that their businessmen are interested in the Sultanate. 
                    “Moreover, on the basis of what we have seen, more would come. 
                    We do know that there is a great potential for our companies 
                    to do business here,” he said. Grabmayr says that Oman offers 
                    vast scope to implement Austrian expertise in various technologies 
                    like renewable energy, information technology, tourism, and 
                    assembling of final goods.<br>
