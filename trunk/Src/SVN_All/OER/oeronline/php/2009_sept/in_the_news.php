                    iCrete is a concrete technology company that has been transforming 
                    the way ready mix and precast concrete is designed, produced 
                    and used worldwide. The iCrete System optimises concrete design 
                    and production for quality, high strength, workability and 
                    durability thereby reducing a project’s cement requirements 
                    and its carbon footprint.<br>
                    <br>
                    <b>Why should a construction company use iCrete technology</b><br>
                    Concrete, the world’s most common building material, has remained 
                    virtually unchanged for centuries. With the ever-rising prices 
                    of cement, steel and labour, however, iCrete technology enables 
                    the production of more cost efficient, higher quality and 
                    eco-friendly concrete. Smarter mixes means less cement and 
                    shortened construction cycles and improved workability. This 
                    also means you need to use less water and fewer materials. 
