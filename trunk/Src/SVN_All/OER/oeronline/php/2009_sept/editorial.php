                    </b> <font face="Verdana" size="1"> <br>
                    An automotive plant brings in a level of ancillarisation unlike 
                    any other industry – the need for components promotes auto 
                    part making; doing up the interiors creates a demand for textiles 
                    and leather; the exteriors require paint; rubber is needed 
                    for tyres, mats and wipers and the list goes on. Multiply 
                    these by the number of people needed to do these chores and 
                    it adds up to a significant number. <br>
                    <br>
                    This is one of the reasons why the industry plays such an 
                    important role in the development of any country. It is hardly 
                    surprising then, that the ascent of countries have been accompanied 
                    by the rise of their automotive industry - the US from the 
                    turn of the 20th century boasted of GM and Ford, post war 
                    Japan’s reputation as a stickler for quality was embellished 
                    by Toyota and Honda, while Hyundai and Kia are doing the same 
                    for South Korea now. <br>
                    <br>
                    Let’s flip the logic – Will a crisis in the automotive industry 
                    affect the fortune of nations? A definite answer to this may 
                    be difficult, but the alacrity with which the Barack Obama-led 
                    US government has acted to salvage GMC and Chrysler underscores 
                    the point. Leaving polemics aside, the truth is that the global 
                    auto industry is going through unprecedented times. Dwindling 
                    sales, vanishing markets, changing preferences, competition 
                    and environment concerns are challenges that are shaking up 
                    even the most formidable of players. Our correspondent Malcolm 
                    Xavier Crasta, an avid follower of the auto industry and the 
                    writer of this month’s cover story says, “The industry has 
                    been through its ups and downs in the past, but never has 
                    the pace of change been so swift or on this scale and that 
                    is what is scary but interesting.” <br>
                    <br>
                    Putting together the story proved to be equally challenging 
                    as it needed months of painstaking research. It was also an 
                    article that required CEOs and senior executives in the Sultanate’s 
                    auto industry to think on a canvas that went beyond their 
                    local businesses.<br>
