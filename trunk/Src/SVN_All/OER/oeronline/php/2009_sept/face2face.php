		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">The English Touch</font></b><br>
                    <font face="Verdana" size="1"><strong>There are few countries, 
                    which are so involved in Oman, like the UK, writes Visvas 
                    Paul D Karra after a tete-a-tete with British Ambassador HE 
                    Noel J Guckian, OBE, and Graham Inett, Head, UK Trade &amp; 
                    Investment</strong></font><br>
                    <br>
                    <font face="Verdana" size="1"><strong style="font-weight: 400"> 
                    <img border="0" src="images/img24.jpg" align="right" hspace="7" vspace="7" width="300" height="243">On 
                    July 23, this year, ‘Al Shamikh’, an offshore patrol vessel 
                    (OPV) of the Royal Navy of Oman (RNO), was named and inaugurated 
                    in London. ‘Al Shamikh’ is one of the three OPV vessels being 
                    built by a London-based company, for the Sultanate of Oman, 
                    as part of the multimillion dollar Khareef Project, launched 
                    a few years ago. The Khareef-class corvette which is expected 
                    to be used for patrolling Oman’s exclusive economic zone, 
                    will be delivered to the RNO before the end of 2010. The last 
                    vessel being delivered in 2012.<br>
                    <br>
                    Says His Excellency Dr Noel J Guckian, OBE, and Ambassador 
                    of Britain to Oman “I was privileged to join HH Sayyid Haitham 
                    bin Tariq Al Said, Minister of Heritage and Culture, at the 
                    launch of Al Shamiq in England. It was a naming ceremony that 
                    was attended by a number of Omanis and I was delighted to 
                    be a British representative there. It is a very good product 
                    for the RNO and we are delighted by the close relationship 
                    that exists between the RNO and the Royal Navy of Britain.”<br>
                    <br>
                    Someone who is familiar with Oman for more than two decades, 
                    HE Guckian has been involved in promoting defence ties between 
                    the two countries since his arrival in Oman (as the ambassador) 
                    in December 2005. The Al Khareef project is an important milestone 
                    in protecting the Sultanate’s marine wealth in its exclusive 
                    economic zone. “In the light of what is happening down south 
                    in the Gulf of Aden (the threat from pirates), you realise 
                    that these vessels are coming in at the right time as you 
                    need to patrol the seas,” he adds.<br>
                    <br>
                    </strong><strong>Offset programme</strong><strong style="font-weight: 400"><br>
                    “An interesting part of the Khareef project is the requirement 
                    from British companies to invest in Oman under an offset programme 
                    called Partnership for Development. British companies will 
                    invest approximately 300mn pounds in Oman as a part of the 
                    commitment,” says HE Guckian.<br>
                    <br>
                    Historically, Oman and Britain share strong ties dating back 
                    to the 17th century. In 1646 a trade agreement was signed 
                    between the two countries giving the British East India Company 
                    access to Sohar. Speaking about bilateral ties, HE Guckian 
                    says that UK has been active in various spheres of the economy 
                    for many decades and believes that a dip in the bilateral 
                    trade during early 2009 has little to do with the global financial 
                    crisis.<br>
                    <br>
                    “We have a general trend of increase in trade between Oman 
                    and Britain although there was a dip in 2006 but this changed 
                    in 2007 and 2008 when there was an increase of 29 per cent 
                    and 62 per cent respectively. This year again, there was a 
                    dip of 17 per cent but the trend overall is positive. It might 
                    be that more trade is going through Jebel Ali in UAE,” he 
                    informs.<br>
                    <br>
                    </strong><strong>Single largest investor</strong><strong style="font-weight: 400"><br>
                    UK is the single largest foreign investor in Oman, with substantial 
                    investments in the oil and gas sector (BP and BG being the 
                    major players). Britain is hopeful that the commitments made 
                    under the Offset Programme will help in diversifying its investment 
                    base in the Sultanate from the oil and gas sector to manufacturing. 
                    For example, Rio Tinto, an Anglo-Australia company has taken 
                    a 20 per cent share in Sohar Aluminium as a part of the acquisition 
                    of Alcan the Canadian aluminium major. <br>
                    <br>
                    The British ambassador spent two weeks in London in July this 
                    year, working on trade and investment relations. During the 
                    course of his stay he interacted with representatives of both 
                    the Middle East Association and the Arab British Chamber of 
                    Commerce. He was surprised to find the high level of interest 
                    amongst British businessmen in Oman. Even those who were not 
                    very familiar with Oman, were keen on investing in the Sultanate. 
                    <br>
                    <br>
                    The UKTI (UK Trade &amp; Investment) is also playing its role 
                    in promoting trade relations. UKTI is the government organisation 
                    that helps British companies by advising them on investment-friendly 
                    countries. The British Embassy recently welcomed Graham Inett, 
                    as head of UKTI, Muscat.<br>
                    <br>
                    </strong><strong>Good business relations</strong><strong style="font-weight: 400"><br>
                    <img border="0" src="images/img25.jpg" align="left" hspace="7" vspace="7" width="250" height="281"> 
                    Inett, who looks after the combined UKTI defence support and 
                    UKTI Civil in Oman, says, “Defence relations between the two 
                    countries are exceptional and in commercial language, good 
                    business is at the heart of this relationship.” Talking about 
                    civil relations, Inett cites the success of the Franchise 
                    Exhibition, held at the Seeb Exhibition Centre in the first 
                    week of August, 2009. The UKTI extended support to this event 
                    with a view to introduce new UK companies to the Omani market. 
                    Traditionally, Oman’s franchise market has been dominated 
                    by the likes of KFCs and Hardees, but now the UKTI Civil is 
                    making concerted efforts to bring big ticket UK franchises 
                    to Oman. Quite a few well known UK chains like BHS, Next and 
                    Mothercare already have a strong presence in Oman. Says Inett, 
                    “The quality of franchises in Oman is very high. Our job is 
                    to alert potential franchises in the UK to come to Oman. We 
                    want to increase awareness about the available opportunities 
                    and are ready to talk to as many people as we can. In addition, 
                    we want to raise the profile of UKTI in Oman.”<br>
                    <br>
                    Apart from manufacturing, the UK is a big participant in the 
                    financial and services sector. Loosely known as ]invisibles,’ 
                    these include banking, accountancy, legal services etc. “There 
                    is considerable British involvement in such invisibles, so 
                    although we might not be involved in specific projects directly, 
                    if you look at any support service for the project, you will 
                    see a British footprint, “says HE Guckian.<br>
                    <br>
                    Inett also cites the construction sector as a good example 
                    of behind-the-line expertise. “Even though a prominent British 
                    company may not be executing a project, on a closer look you 
                    may find that the support services like architecture consultancy 
                    is being provided by a UK based company.” For instance, the 
                    design consultant for the Sohar Port was Halcrow, a British 
                    company; and UK’s Theatre Lighting Company is involved with 
                    the Opera House which is under construction at Shatti al Qurum. 
                    Several other British companies are also actively involved 
                    in the Opera House construction in various capacities. The 
                    upcoming Botanical Gardens in Seeb is using expertise from 
                    the Royal Botanic Garden of Edinburgh (RBGE) – the world-renowned 
                    centre for plant sciences and education. <br>
                    <br>
                    </strong><strong>Educational ties</strong><strong style="font-weight: 400"><br>
                    The 7,000 plus strong British community in the Sultanate is 
                    making a contribution to almost eve<img border="0" src="images/img26.jpg" align="right" hspace="7" vspace="7" width="300" height="175">ry 
                    sector of the economy, and is the largest western community 
                    in Oman. This is a measure of the strong people-to-people 
                    contact that exists between the two nations. Education is 
                    another cornerstone of bilateral relations. In 2008, there 
                    was a 42 per cent increase in number of Omani students enrolled 
                    in British universities. This roughly translates to over 2,000 
                    students going to the UK for higher studies each year. The 
                    UK attracts the fourth largest group of Omani students going 
                    abroad for higher studies each year after UAE, Jordan and 
                    Egypt.<br>
                    <br>
                    According to HE Guckian over 20 British universities and higher 
                    education institutions have either affiliates or programmes 
                    running in Oman. For example, the Caledonian College of Engineering 
                    is affiliated to Glasgow Caledonian University in the UK. 
                    Over 900 Omani teachers of English in state schools completed 
                    a graduation programme from Leeds University in June 2009. 
                    These teachers, earned a Bachelor of Arts in teaching English 
                    to speakers of other language (Tesol). Almost every Omani 
                    English teacher in the state schools has a British degree, 
                    discloses HE Guckian.<br>
                    <br>
                    The British Council in Oman, conducts several socio-educational 
                    and cultural programmes promoting bilateral ties.<br>
                    <br>
                    </strong><strong>Foreign policy</strong><strong style="font-weight: 400"><br>
                    The UK greatly values Oman’s advice on foreign policy issues 
                    like Iran, Iraq, Yemen and the Middle East peace process. 
                    Though Oman and UK share a historical relations, these are 
                    being cast in a contemporary mould, by the efforts of people 
                    like HE Guckian. While concluding, HE Guckian says, “We never 
                    sit on our laurels and are constantly looking at ways to enhance 
                    our relationship at all levels. It can be at the youngest 
                    levels through British Council, or at the highest levels where 
                    we rely heavily on Oman and the government of His Majesty 
                    Sultan Qaboos bin Said for advice on foreign policy in the 
                    region.”
                    </strong></font>
