                    </font><b><font face="Verdana" size="4">CORAL MANAGEMENT NEED 
                    OF THE HOUR</font></b><font face="Verdana" size="1"><br>
                    <b>Coral reefs are an important component of the marine eco-system 
                    and their conservation is imperative to sustain marine wealth</b><br>
                    <br>
                    Across the world, nearly 109 countries have significant coral 
                    reef communities and 85 per cent of these countries are experiencing 
                    damage to their coral reefs. The threats are both local factors 
                    such as overfishing and coastal pollution, as well as other 
                    factors like global warming and ocean acidification. Coral 
                    reefs are much more than a pretty picture, they are important 
                    assets. Co<img border="0" src="images/img57.jpg" align="right" hspace="8" vspace="6" width="141" height="135">ral 
                    reefs in some parts of Southeast Asia are under pressure by 
                    intense destructive fishing and coastal pollution. In this 
                    case, local factors dwarf the effects of global factors such 
                    as global warming and ocean acidification. In other parts 
                    of the world, where pressures from local sources are lower, 
                    reefs are still declining at the rate of about 1-2 per cent 
                    per year.<br>
                    <br>
                    <b>Coral protection</b><br>
                    The Sultanate of Oman offers incredible dive locations abounding 
                    with fascinating marine environment and beautiful coral reefs. 
                    However, divers and fishermen often anchor their boats in 
                    areas where fragile coral reefs exist. Oman has launched a 
                    number of private-public measures to protect the coral reefs. 
                    For instance, mooring buoys have been installed in certain 
                    areas to protect reefs from the danger of anchors which are 
                    let down by tourist boats. Further, coral cleaning is another 
                    important movement in the Sultanate. These initiatives are 
                    aimed at protecting and preserving the Sultanate’s coral reefs.<br>
                    <br>
                    According to a report by the World Bank, the deterioration 
                    of coral reef ecosystems is a visually poignant reminder that 
                    climate change is not just an environmental issue but a development 
                    issue. Developing countries need support not only to mitigate 
                    future deterioration of their coral reefs, but also to adapt 
                    to a sustainable use of the diminishing returns these reefs 
                    are likely to provide.<br>
                    <br>
                    <b>Economic casualty</b><br>
                    Major new research indicates that coral reefs will not survive 
                    the rapid increases in global temperatures and atmospheric 
                    CO2 that are forecast this century. The livelihoods of more 
                    than 100 million people living along the coasts of tropical 
                    developing countries will be among the first casualties of 
                    the loss of coral reef systems.</font>
<p align="justify" style="margin: 3px 10px" dir="ltr">
                <font face="Verdana" size="1"> <br>
                Developed countries must drive down global emissions, spearhead 
                the development of new technologies and sponsor the adaptation 
                of developing countries. The latter is very important given that 
                developing countries have often signed international treaties to 
                pursue better management of their coastal resources, yet often 
                have conflicting priorities and little financial resources or 
                capacity to act on these commitments. <br>
                <br>
                There is a responsibility for high carbon growth countries to 
                make funds available to assist the most vulnerable coral reef 
                states, who need support in adapting to climate change impacts 
                while reducing local risks to reefs. At the local level, there 
                needs to be immediate action to stop over-fishing, coastal 
                pollution and unsustainable coastal development in the 
                developing world.<br>
                <br>
                The Coral Reef Targeted Research and Capacity Building for 
                Management Programme (CRTR), a global movement, was conceived to 
                shed light on the impact of climate change and other human 
                stressors on coral reefs through coordinated research, and to 
                put this knowledge into the hands of decision-makers, thus 
                improving management options and policy interventions. <br>
