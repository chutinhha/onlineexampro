                    <font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4" color="#C00000">
					Affordable exclusivity<br>
					<br>
					</font><font face="Verdana" size="2">Country Club changed 
					the concept of clubbing in India by bringing it to the reach 
					of families. Y Rajeev Reddy, Chairman and Managing Director 
					tells Visvas Paul D Karra about the company’s foray into 
					Oman and the way ahead</font></b><p align="center" style="margin:3px 10px; " dir="ltr"> 
				<img border="0" src="images/2010_july_companyprofile1.jpg" style="border: 2px solid #C0C0C0" width="400" height="266"><p align="justify" style="margin:3px 10px; " dir="ltr"> 
                    <font face="Verdana, Arial, Helvetica, sans-serif" size="1">
					<br>
					<br>
					One of the endearing images of Country Club India (CCIL), 
					India’s largest leisure infrastructure company, is of Y 
					Rajeev Reddy, the company’s chairman and managing director 
					who appears in the promo advertisements with his trademark 
					thumbs-up sign. The thumbs-up sign which often suggests a 
					positive attitude, happiness, good spirit, or a sense of 
					accomplishment could well symbolise the life of Rajeev Reddy 
					who began with a modest real estate company – the Amrutha 
					Estates – and is today at the helm of one of the fastest 
					growing entertainment and leisure conglomerates in India.<br>
					<br>
					<b>Family clubbing<br>
					</b>A multi-million dollar entity and a listed company on 
					BSE (Bombay Stock Exchange), CCIL is a pioneer in the 
					concept of family clubbing in India and has established 205 
					properties of which 50 are owned and 155 are franchised. It 
					has over 172 affiliations plus a global gateway via Country 
					Vacations and RCI affiliation of 4000 resorts for its 
					esteemed members. For the year ended March 31, 2010, CCIL 
					reported net sales of RO40.37mn and its consolidated net 
					profit was RO6.5mn.<br>
					<br>
					The Limca Book of World Records has recognised CCIL as 
					India’s biggest chain of family clubs. With 51 of its own 
					clubs, it is truly a pioneer in family clubbing offering 
					state-of-the-art facilities in all its clubs for heightened 
					pleasure.<br>
					<br>
					All CCIL properties are set amidst sprawling green 
					environments with swimming pools, indoor games, health 
					clubs, well-serviced rooms and professionally trained 
					manpower to meet everyone’s need. The clubs are also 
					equipped to handle banquets, entertainment and business 
					needs where you can organise weekend activities, 
					competitions and contests to engage and build a clubbing 
					community. A unique benefit to members joining Country Club 
					is the facility of transferability of membership from one 
					city to another, paying the differential membership fee, in 
					case the fee at the city to which transfer is sought is 
					higher.<br>
					<br>
					<b>Regional expansion<br>
					</b>CCIL, which has a presence in UAE, is now expanding 
					across the region and has opened a new office Country 
					Vacations International in Muscat in June, with an objective 
					to promote and sell Country Club Memberships. The office is 
					located at Royal Tower, Near Centre Point in MBD Area. In an 
					exclusive interview with Oman Economic Review after the soft 
					opening of the office, Reddy said, the Sultanate of Oman is 
					an ideal location for the Country Club group to venture into 
					as they intend to cater to expat Indians across the region, 
					while at the same time raising the brand awareness among the 
					local community.<br>
					<br>
					“We see immense potential for the growth of tourism and our 
					office in Muscat, Oman, is an entry point for us to 
					introduce the group membership offers and we are certain of 
					attaining our set targets for this region,” Reddy said.<br>
					<br>
					Apart from the club membership, CCIL has introduced the 
					concept of Fractional Ownership of buying a portion of an 
					expensive asset like CCIL properties. A percentage share of 
					the luxury asset is divided amongst the owners. As a 
					fractional owner the member can enjoy the priorities and 
					privileges, such as using the property as his or her holiday 
					destination, while his asset increases in value.<br>
					<br>
					<b>Focus areas<br>
					</b>Having achieved an enviable status as a premier family 
					clubbing chain, Reddy says that now Country Club is intent 
					on consolidating its position as a niche leisure provider. 
					For this, four core thrust areas have been identified as 
					part of their expansion plans.<br>
					<br>
					The first is wildlife holidays through Country Club India’s 
					Wildlife Resort Bandipur, an exotic resort situated on the 
					outskirts of Bangalore. This property is nestled in the 
					Mangala Valley and surrounded by the lush forests of 
					Bandipur, a protected sanctuary and home to the most exotic 
					fauna and flora.<br>
					<br>
					<b>Adventure, wellbeing<br>
					</b>The second focus area is intended for those who have the 
					adventure streak in them. CCIL’s 155 clubs combined together 
					create a spirited entanglement of adventure sports to bring 
					out the high-end adrenalin rush. Country Clubs situated at 
					various geographic locations offer premium adventure sports 
					facilities to its members. These include the best rapids in 
					India, white water rafting in Sri Lanka (Country Club 
					Babylon); paragliding, scuba diving and snorkelling in Goa 
					(Country Clube de Goa); and mountaineering and light 
					aircraft gliding in Nepal.<br>
					<br>
					Medical tourism is the third area in which Country Club has 
					invested considerable resources and has plans to market its 
					facilities all over the world. The Country Spa; and Country 
					Club Treasure Cove, Asia’s first floating Spa – both located 
					in Kovalam, Kerala have been specially crafted for providing 
					Ayurvedic rejuvenation therapies.<br>
					<br>
					“Since naturopathy is increasingly becoming popular among 
					the urban populace, the two facilities have specially 
					trained staff to provide complete medical therapy. They are 
					aided by the fully equipped Ayurvedic treatment centre, 
					pharmacy, Ayurvedic restaurant, yoga hall; curio, library, 
					herbal gardens along with Ayurvedic cooks and dietician for 
					a revitalising stay,” informs Reddy.<br>
					<br>
					Leisure Infrastructure is the fourth focus area. CCIL is the 
					fastest growing leisure infrastructure company in India and 
					its foray into this niche segment began with the very first 
					leisure infrastructure project ‘Country Club Coconut Grove’ 
					near Bangalore. Similar projects across India include 
					Country Club Mango Grove, Chennai; Country Club Bamboo 
					Grove, Lonavala, Mumbai; Country Kuteeram, Lepakshi, 
					Hyderabad etc.<br>
					<br>
					CCIL has further identified markets and regions like 
					Thailand, Malaysia, Europe and Africa along with various GCC 
					countries for its global expansion. The Country Club Hotel 
					in the UAE is part of the RO100mn strategic expansion plan 
					of the group in the region.<br>
					<br>
					<b>Outgoing personality<br>
					</b>Reddy, a graduate in commerce, decided to venture into 
					real estate and construction when he was 21 years old with 
					Amrutha Estates named after his mother in the year 1981. The 
					first ambitious project he undertook was Ashiana, a 
					residential complex consisting of 20 deluxe flats, in the 
					capital city of Andhra Pradesh. Since then he has completed 
					38 prestigious projects in Bangalore and Hyderabad.<br>
					<br>
					Realising that many clubs make it difficult for common 
					people to obtain membership, Reddy started the Country Club 
					in 1989, which now stands as a landmark in the city of 
					Hyderabad. His motive has been to create a club that is 
					accessible and affordable for all sections of society.<br>
					<br>
					The 52-year-old Reddy is a fitness enthusiast who loves 
					swimming and was a state tennis champion while studying. He 
					also enjoys outdoor sports like paragliding, river rafting 
					and scuba diving. Reddy has two sons and a daughter who are 
					also the board members of CCIL. </font><p align="center" style="margin:3px 10px; " dir="ltr"> 
