				<br>
				<br>
				In one way, it was the meeting of theatre and art, with the 
				common thread being creativity. Ashok Suvarna is a passionate 
				stage artiste and theatre personality with more than 50 
				commercial and non-commercial plays to his credit. As the 
				executive director of NPA Events, Suvarna has been involved in 
				organising a number of events, included notable plays.<br>
				<br>
				Therefore it was a natural progression when Al Salmi Library 
				wanted to bring Rembrandt’s paintings and etchings for display 
				in Oman, Suvarna’s NPA Events was chosen for the job, knowing 
				that passion and creativity would come together befitting the 
				17th century artist.<br>
				<br>
				The result was that Oman’s art connoisseurs were left breathless 
				to see in flesh and blood more than 100 original etchings by the 
				world-renowned Dutch artist Rembrandt Harmenszoon van Rijn. The 
				event called “Rembrandt in Oman” was organised at the Grand 
				Hyatt Hotel from August 19-September 19 in 2009.<br>
				<br>
				<b>Winner laurels<br>
				</b>For organising this once in a lifetime exhibition, NPA 
				Events bagged the first prize in the Best Arts and Cultural 
				Event category at the Middle East Event Awards in Dubai in May 
				this year, beating other contenders. <br>
				<br>
				However, the organisation of this event was no easy task and it 
				was only the tenacity, determination and dedicated efforts of 
				NPA Events team, which made it possible. The Afrah Ballroom of 
				Grand Hyatt was converted into an art gallery within a short 
				period of just four days employing more than 65 people who 
				worked non-stop day and night to complete all the settings 
				including lighting, security, flooring etc. A 4-metre high 
				facade resembling Rembrandt’s house was also erected to complete 
				the feeling of entering the Dutch maestro’s house. In addition, 
				dehumidifiers imported from Dubai were installed in the gallery 
				area to take care of the humidity issue and the preservation of 
				the prints.<br>
				<br>
				“We at NPA Events believe in perfection and excellence, 
				therefore, we work with passion to achieve that. At the end of 
				the event, the entire team looks carefully into everything to 
				see what is imperfect in that particular event. Then we try to 
				assess what was ordinarily done which could have been done 
				extraordinarily, and then rectify the same in our next event. It 
				is an ongoing process for us. But we aim to create the ‘wow’ 
				factor in every event we handle,” says Suvarna, while talking 
				about the memorable event.<br>
				<br>
