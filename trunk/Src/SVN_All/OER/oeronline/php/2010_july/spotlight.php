				travel<br>
				</font></b><font face="Verdana" size="2"><b><br>
				For a travel insurance, it is always important to choose an 
				insurer who has a global presence to support you while you are 
				abroad says Hemant Gupta, Regional Manager-Travel, Axa Insurance<br>
				<br>
				</b></font><font face="Verdana" size="1"><b>What are the best 
				ways to choose a right travel insurance policy?</b><br>
				Contrary to other insurance products, make sure you choose the 
				product that offers a protection on different areas of risks. 
				Basically a package of solutions for personal accident, personal 
				liability, emergency medical expenses for in-patient and 
				out-patient covers, travel related exposures such as luggage 
				delay/losses, accidental loss of money, important documents and 
				personal belongings as well as trip cancellation/curtailment 
				following insured perils. Another side of the story is to choose 
				the right insurer that owns a direct global presence through 
				branches and operations within the country of destination and 
				capabilities to service inquiries and claims by the 24/7 
				approach.<br>
				<b><br>
				What are the biggest myths about travel insurance?</b><br>
				The moment of truth generally happens at the time of the claim. 
				You need to be sure that your insurer has the appropriate 
				infrastructure that can help you to find the necessary support 
				at the right time and in the right place. When you are abroad, 
				you are in unfamiliar surroundings and it is important to 
				eliminate complications through a clear and simple process so 
				that you as a customer have easy access to the insurer together 
				with a clear set of procedures to submit a successful claim. It 
				is important to know what has to be done following a loss with 
				regard to the time of notification, loss declaration, 
				determination of proximate cause as an insured peril whether 
				connected or not with the chain of events that have finally 
				caused the loss or losses and the collecting the required 
				official documents etc.<br>
				<b><br>
				What are the biggest mistakes people make when it comes to 
				travel insurance?</b><br>
				They miss the fact that travel insurance is not just a product 
				but also the ability of insurer to provide a global network of 
				support to efficiently handle claims in terms of quick time 
				response and quality of service.<br>
				<b><br>
				How badly did the recent flight disruptions caused by Iceland’s 
				volcanic ash affect travel insurers, especially UAE-based? And 
				how are you facing the situation?</b><br>
				There is no doubt that the said disruption has created a 
				negative impact and stopped people from catching-up on their 
				obligations and pre-arrangements at both business and personal 
				levels. However, it is important to have clarity in terms of 
				what is covered, what is not covered and what is excluded to 
				measure the expected losses sooner than later.<br>
				<b><br>
				Does travel insurance provide coverage against natural 
				calamities?</b><br>
				Natural calamities are generally excluded from any travel 
				insurance coverage.<br>
