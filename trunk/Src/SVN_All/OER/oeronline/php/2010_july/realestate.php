                    </font><b><font face="Verdana" size="2"><br>
				Professional property valuers like Al Habib use cost plus profit 
				and income capitalisation methods to arrive at the fair value of 
				a property<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				<b>Recently one of our clients told us that he was looking to 
				make a small investment in real estate by buying an income 
				yielding building.</b> We told him that the sellers were asking 
				for prices based on the present income of the building. Any 
				building generates income over many years and if the value is to 
				be based on capitalisation of the rental revenue stream, one 
				must have a good idea of where rents are headed in the years to 
				come. A quick and easy way to find out if price is high is to 
				compare the value of a building arrived at using the income 
				(based on present rents) capitalisation method with the cost 
				method. After all, any investor would not be willing to pay much 
				more than what it would cost him if he bought a similar piece of 
				land in a similar location and constructed a similar building. 
				He would, of course, pay a premium, called the entrepreneurial 
				profit, in order cover for time value of income and the risks 
				involved in building (costs could go up, a contractor could 
				default on his commitment, quality tenants may not be found 
				etc.). However this premium should be “reasonable” and 
				professional property valuers have a good estimate what this is 
				at various points of time. <br>
				<br>
				<b>Calculating gains <br>
				</b>When these “premiums” are too high and as the cost plus 
				profit method and income capitalisation method have to converge, 
				something has to give way, which means that either costs have to 
				go up or rents have to go down. Costs could go up if commodity 
				and labour costs move up and this depends on global factors like 
				the growth in the global economy which drives commodity prices 
				and inflation which could be triggered by quantitative easing 
				(in layman’s language printing money) that many central banks 
				are resorting to. However at the moment investors are not 
				betting on inflation in the short term although “slight” 
				inflation is always targeted by central banks and is always 
				going to be there in the long run since deflation can be 
				catastrophic. Typically, these “premiums” depend on the economic 
				cycle with premiums being high in boom phases and low (could be 
				negative) in downturns.<br>
				<br>
				Another likely scenario is that rents will fall. Therefore, 
				investors must be cautious while buying real estate assets and 
				must ensure that there is not too much of a gap between the cost 
				and income capitalisation methods. It is, of course, possible 
				that costs themselves could drop but investors have to take a 
				view on this based on global and national economic factors. <br>
				<br>
				Rents depend on supply and demand but ultimately developers will 
				continue building as long as the returns are attractive meaning 
				rents are high and therefore, supply will keep rising when there 
				is a gap between the income capitalisation and cost methods. 
				Thus in the long run the two methods of valuation have to 
				converge and any divergence is a signal to buy or sell. </font>
