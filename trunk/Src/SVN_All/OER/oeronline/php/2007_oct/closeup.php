<img border="0" src="cover12.jpg" width="350" height="209"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
<b>Two-year limit<br>
</b>It seems the logic behind limiting the visa to two years is meant to ensure 
that foreign nationals, who obtained the residency for owning property, remain 
owners. Under the law, expatriates are not allowed to trade in property or to 
accept job offers. As such, those who violate the requirements would not have 
their residency permits renewed. It is against this backdrop that proponents 
contend that locals should not suffer from the law. <br>
<br>
To be sure, Bahrain is not targeting expatriates currently working in GCC 
states. These people maybe better off going to their home countries for 
retirement or other purposes. It is assumed many of these people could not 
afford living expenses in Bahrain. Like elsewhere in the GCC, living expenses 
have been on the rise in Bahrain over the last few years. In fact, owning a 
property is increasingly becoming difficult for locals and many Bahrainis seek 
government-assisted housing units. <br>
<br>
Conversely, authorities are seeking to woo well-to-do expatriates who would like 
to live in Bahrain for what the country offers – a unique quality of life. 
Bahrain is small in area (about 700 square kilometres) but offers all amenities 
of modern-day life. Also, while most locals tend to be conservative, the 
authorities have embraced liberal economic policies. No wonder, many Saudi 
nationals tend to visit Bahrain during the local weekend to enjoy the country’s 
open and more tolerant environment.<br>
<br>
<b>Negative aspects<br>
</b>The policy though has its sceptics who fear that there will be additional 
inflationary pressures through increased spending and demand on housing by 
foreign nationals. Currently, inflation is at about 5 per cent per annum. 
Bahrain imports most of its needs and hence is a price taker. Over the last few 
months, it witnessed shortage of red meat. It was blamed on declining supply 
from Australia, caused by relatively long drought Down Under.<br>
<br>
Also, opponents stress that foreign nationals, like locals, stand to be benefit 
from subsidised goods and services such as electricity, water and petrol prices. 
Petrol is sold in Bahrain at 100 fils ($0.26) per litre, which is considerably 
below prevailing prices in many parts of the world, notably European countries 
such as France and Britain. In fact, the parliament has started discussing 
proposals to limit subsidised products by the government to needy people. One 
such proposal is to provide petrol coupons to locals who are finding it 
difficult to meet their needs. <br>
<br>
On a more convincing note, opponents argue the law could only worsen population 
density problem in Bahrain. Bahrain already has the highest population density 
for any country in the region with more than 1,000 people per sq. km. Only a 
handful of other nations, notably Singapore and the Maldives, outnumber Bahrain 
in population density. <br>
<br>
No date was set for implementation of the decree that proved controversial from 
the onset. Not surprisingly, several members of parliament have called on the 
authorities to present the new policy before the legislative body for 
deliberations and eventual voting. These MPs, including me, argue that the 
parliament should have a say in the matter since it represents a new policy. 
Likewise, civil societies have demanded the opportunity to discuss implications 
of the policy prior to its implementation. Once implemented, Bahrain needs to 
review implications of the law on a regular basis and make necessary amendments 
when and if required.<br>
<br>
Bahrain is cognisant of the importance of globalization. Their early adoption of 
such a policy sends a welcoming message to citizens of all nations.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="right" style="margin: 3px 10px">
