<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Bahrain woos foreign nationals<br>
</font></b><font face="Verdana" size="1"><b>Bahrain is keen to gain an edge over other Gulf nations, especially the UAE 
and Qatar, by making the kingdom uniquely receptive to expatriates, writes Dr 
Jasim Husain Ali<br>
</b><br>
<img border="0" src="cover11.jpg" width="125" height="237" align="left" hspace="6" vspace="4">Bahrain is increasingly becoming attractive for foreign nationals. On August 12, 
the Bahraini cabinet allowed foreign nationals the right to live in Bahrain for 
two years under self-sponsorship visa. What’s more, the visa can be renewed 
indefinite number of times.<br>
<br>
The special permit will be granted to expatriates who meet any one of the three 
options: having worked for at least 15 years in any Gulf Cooperation Council (GCC) 
country whether in public or private sector; owning property in Bahrain worth at 
least BD50,000 (about US$132,275); or, investing a minimum of BD100,000 in the 
country. Investment choices include in commercial, industrial, educational, 
training, tourist or financial services.<br>
<br>
Bahrain is keen to gain an edge over other Gulf nations, especially the UAE and 
Qatar, by making the kingdom uniquely receptive to expatriates. It is argued 
that the law would help bring in more wealth into Bahrain and hence give a 
fillip to the country’s economy, including creating jobs for locals. 
Unemployment is a key economic challenge facing Bahrain and as such, the policy 
is designed to kill two birds with one stone – expanding Bahrain’s gross 
domestic product (GDP) and providing more employment opportunities for locals.
<br>
<br>
<b>Economic benefits <br>
</b>Undoubtedly, Bahrain’s new liberal policy stands out in the GCC. While in 
the kingdom, foreign nationals would spend money through either renting or 
owning property. Also, they would endure living expenses on foodstuff, 
electricity, water and telecommunications. This is what the economists call, the 
multiplier effect of money. On an average, every single dinar (about $2.65) 
creates four additional dinars in the economy through multiplier effect. So 
Bahrain’s economy stands to gain substantially from foreign nationals coming to 
the country via the new policy.<br>
<br>
Of all the economic sectors, the construction sector is expected to be the main 
beneficiary. The expatriates have to either own or rent a property. Undoubtedly, 
growth of the construction sector has a positive spillover onto other sections 
of the economy. <br>
