<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The Dunes, a brand in itself<br>
</font></b><font face="Verdana" size="1">
<b>The Dunes in Dubai is set to be positioned among the world’s finest golf 
clubs in terms of course design and membership flexibility</b><br>
<br>
<img border="0" src="golf1.jpg" width="150" height="136" align="left" hspace="6" vspace="4">Playing golf and managing a business–they have a lot of similarities. Both 
demand concentration, discipline, experience and patience. No wonder, most top 
managers and business persons have a penchant for playing golf in their leisure 
to rejuvenate themselves and at the same time keep themselves mentally agile. 
The Dunes golf course in Dubai is a common meeting ground for many such industry 
captains and golfers from across the globe. Victory Heights Golf Residential and 
Development LLC is now building a modern metropolis in and around it. Golf is 
not just about athletic competition; it is also about enjoying the game and 
living life at one’s own pace.<br>
<br>
Nestled in 28 hectares of rolling dunes and native vegetation, The Dunes 
combines a links-style course with traditional classic-era design. World golfing 
champion Ernie Els originally designed this 18-hole signature course.<br>
<br>
In recent times, Victory Heights Golf Residential and Development LLC, has 
officially taken over The Dunes from Septech Emirates LLC. Victory Heights, an 
extension of a joint venture between the Dubai Sports City and Arcapita (a 
Bahrain-based investment bank), represents a very modern lifestyle concept. 
Covering some 25 million sq. ft, this private golf course villa community will 
feature 935 luxury villas and townhouses in European and Mediterranean classical 
style, set in and around The Dunes. <br>
<br>
As a result of this takeover, Troon Golf, reputed for luxury-brand golf course 
management, marketing and operations, will begin its full pre-launch maintenance 
operations prior to official opening of the golf course at the end of this year.<br>
<br>
<img border="0" src="golf.jpg" width="300" height="363" align="right" hspace="6" vspace="4">After the renovation, The Dunes will provide the exclusive Victory Heights villa 
community not only with good sporting and recreational facilities, but also with 
a visually attractive landscape embellished with lush green lawns and tranquil 
lakes. In addition, facilities within Victory Heights will include schools, 
community centres and shops. The exciting sporting, leisure and entertainment 
facilities of Dubai Sports City are within walking distance.<br>
<br>
The game of golf will be further be complemented by the Butch Harmon School of 
Golf, the first such academy outside the US, and a luxury Mediterranean-style 
golf clubhouse for interaction and entertainment. The David Lloyd Next 
Generation Club and Tennis Academy, the first of its kind in the Middle East, is 
also on the doorstep. The Next Generation Club is a health and country club 
offering a wide range of fitness facilities including a state-of-the-art gym, 
swimming pool, tennis and squash courts, yoga studios and a spa. <br>
<br>
The Dunes will also adopt the transferable membership concept so as to uniquely 
protect and reward its members. Under the transferable membership model, 
individuals, families, and corporations will be invited to pay a one-time fee to 
become a member of the club. The transferable membership can then be sold at a 
later date at market value, for example if the member is leaving the country. 
Members will also pay an annual fee in line with current Dubai golf club annual 
membership rates.<br>
<br>
The obvious benefit, according to the club’s management, is that the 
transferable membership is likely to retain or even increase its value over time 
as the number of memberships at the club will be limited.<br>
<br>
Yasser Abdulla, general manager of Victory Heights, is optimistic about the 
changed face of The Dunes and says, “The Dunes will be one of the world’s most 
exciting and challenging courses.” </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
