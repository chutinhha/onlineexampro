<img border="0" src="worldscan1.jpg" align="left" hspace="6" vspace="4">Ad-Air, the unique global advertising medium that targets airline passengers, 
was launched recently in London. As the world’s first international network of 
flight path advertising sites, Ad-Air offers brands unprecedented exposure to a 
huge global consumer audience via digitally printed 20,000m² advertisements 
placed along the runways of the world’s busiest airports. Each advert is 
equivalent to nearly three times the size of Manchester United’s Old Trafford 
pitch or twice the size of London’s Trafalgar Square. <br>
<br>
Ad-Air Group Plc, backed by £5m of private equity finance, spent the last five 
years researching and securing appropriate sites from private landowners around 
the world’s busiest airports across Europe, North America, Middle East and 
Asia-Pacific. These include London’s Heathrow, and the Paris, Madrid, Geneva, 
Atlanta, Denver, Los Angeles, Phoenix, Tokyo, Beijing, Bangkok, Abu Dhabi and 
Dubai airports, where Ad-Air’s first site and advertiser will be unveiled soon. 
The Ad-Air sites offer advertisers the opportunity to reach a high quality 
captive audience of typically ABC1 airline passengers, including many otherwise 
hard to reach business travellers, at a moment when they have little else to 
distract them. <br>
