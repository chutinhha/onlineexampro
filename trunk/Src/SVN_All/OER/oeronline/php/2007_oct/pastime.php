<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">News-and-views buff<br>
</font><font face="Verdana" size="1">&nbsp;</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1"><b>Ajay Ganti, General Manager, Al-Seeb Technical Est. (SARCO) tends to occupy 
himself with any activity that increases his intelligent quotient and takes his 
learning curve upwards.
Some music and movies help to lighten the mood</b><br>
<br>
No one actually sits and thinks about how they like to relax, so its only 
natural that when you are asked to pinpoint some of your favourite leisure 
activities, it does take a minute or two to collect your thoughts. But having 
done this, Ajay Ganti, General Manager, Al-Seeb Technical Est. (SARCO) is all 
ready with his answers, and we find there are numerous ways by which he spends 
his free time.<br>
<br>
Ajay says, “I unwind watching television – mainly business news, talk shows and 
a lot of movies. My type of movies would be action, English espionage films, and 
comedy movies that have sarcastic humour.”<br>
<br>
“Also music is my weakness, I listen to classical Indian music and old time 
Hindi songs to relax. Besides this I enjoy attending concerts and love Urdu and 
Hindi poetry by Galib, Firaz and Nida Fazli.” He likes hearing music composed by 
famous Indian musicians – Shankar Ehsaan Loy, Jatin Lalit and R.D Burman, an-all 
time favourite, and of late, Shantunu Moitra and Vishal Shekhar’s music holds 
great appeal.<br>
<br>
Being in a profession that requires constant awareness of the happenings in the 
market, Ajay keeps abreast by reading business articles which help him 
understand strategies adopted in the business environment. He also reads 
management related books and is a regular subscriber of the magazine Mackenzie 
Quarterly. Talk shows on television is another favourite of his, but this person 
is only interested in business and politics, not petty social issues and the 
like, so he favours shows like Walk the Talk, We the People, Witness and Hard 
Talk. <br>
<br>
When a person’s interest lies in business and politics, it’s evident that global 
and political news would follow on the list. “I keep a track of the news and 
current events because the business environment is determined by politics, 
especially in places like India,” he says.<br>
<br>
The internet too plays a small role in entertaining Ajay, “I check business 
articles on the internet, and I sometimes indulge in blogging with people from 
the corporate world, I tend to occupy myself with any activity that increases my 
intelligent quotient and takes my learning curve upwards” he emphasises. For 
Ajay, partying is one of the best ways to relax and socialise but here in 
Muscat, he says, “I have yet to build up my social circle here since I am a 
relatively new resident of the place and have left my party culture behind in 
Delhi, India.”<br>
<br>
Being engaged in a busy daily schedule, this athlete does not find the time to 
indulge in any sporting activity, but realising that a sedentary lifestyle will 
be harmful in the long run; Ajay has now adopted skipping in his daily routine 
to keep fit. Occupied as he may be with work, he does not compromise on having a 
healthy sound sleep, and says, “Proper sleep is the key to good health; I love 
to sleep in my free time too.”<br>
<br>
The weekends are time for the family to go out together, to spend time with his 
kids, and shopping for household items. “It is the only time my wife gets to 
relax as we visit eating joints and though the first six months of arriving in 
Oman was dedicated to settling down, I would now like to visit Salalah and Jabal 
Shams among other places, I am also looking forward to taking my kids for 
snorkelling and diving in the Dive Centre,” he says. </font>
