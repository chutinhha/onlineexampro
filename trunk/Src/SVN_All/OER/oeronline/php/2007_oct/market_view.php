<b>Galfar IPO Research Abstract</b><br>
Galfar’s IPO is coming at a time when the company is embracing yet another 
high-growth trajectory, which should move in tandem with overall infrastructure 
development and construction activity in Oman. Given the buoyancy in the economy 
and post-cyclone restoration efforts, it can be believed that the spending on 
infrastructure and new industries by the government and other private players 
will gather momentum in the days to come. As per 2007 budget, RO500 million 
worth of infrastructure development projects (non-oil) is envisaged, and an 
additional RO135 million is earmarked for the electricity sector. Strong order 
book position of RO600 million and a healthy conversion rate (historically about 
35-40 per cent) makes us believe that the company will be able to achieve its 
projected financials with much ease. <br>
<br>
Though the tenders are yet to be awarded, the company expects that it is the 
lowest bidder in contracts worth over RO150 million. The financial projections 
for 2007 for Galfar indicate 46 per cent growth on top line (40 per cent of 
projected revenues achieved up to May 2007) and 30 per cent growth in bottom 
line (39 per cent of projected profits achieved up to May 2007). As new orders 
get added to the book, we expect the projections for 2008 and 2009 to be 
upgraded. At an EBITDA multiple of 4 times and PE multiple of 7 times on its 
2007 estimates, we believe the Galfar offering is priced attractively for 
investors.<br>
<br>
<b>Renaissance Services SAOG Research Abstract </b><br>
Renaissance Services (RNS) registered a y-o-y growth of over 35 per cent in its 
top line. On a q-o-q basis, the growth has been over 24 per cent mainly driven 
by the marine services group, which contributes roughly 32 per cent of the 
company’s total revenues. The Contract Services Group too witnessed good 
additions to top line through Norsk Offshore Catering, which RNS acquired during 
Q3 FY06. Operating margins declined due to the lower impact of ‘contract 
services’ business; in Q207, though, there was some improvement. The company’s 
business model has turned better with increased visibility in earnings from a 
sustainable stream of revenues. Prominent in the new business model, the Marine 
Services segment is expected to ease out the volatility in earnings arising from 
the unsustainable Contract Services business, which used to be the key profit 
driver for the company even up to the first half of FY05.<br>
<br>
Current capex layout of roughly US$500 million (RO192 million) should push RNS 
into a bigger league in the marine and oil and gas services business. The 
company has also been able to successfully diversify its core profit-stream from 
the high-risk contracts business to more sustainable and stable businesses like 
marine services. Management’s strategy to have a blend of asset-based and 
service-based businesses within the same fold has been working well for RNS 
during this period of metamorphosis. Assuming significant growth in marine 
services group and following current capex plans, our model works out an EPS of 
RO0.068 for FY07 and RO0.077 for FY08. Current valuations discount these 
estimates by 10.5 and 9.3 times. Given the positive business outlook and low 
valuations with respect to growth, Vision maintains a ‘buy’ rating on the stock.
