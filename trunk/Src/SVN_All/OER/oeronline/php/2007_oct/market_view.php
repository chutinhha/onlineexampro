<font size="1"><br>
<b>GCC Market<br>
</b>The markets ended mixed for the one-month period ended September 15. The 
Doha bourse was the top gainer and the DSM index increased by 4.32 per cent to 
7716.53 points during the period. All the major indices on the exchange ended on 
a positive note, with the industrial and banking indices rising by 5.75 per cent 
and 4.42 per cent, respectively. The Kuwaiti index, which has the highest year 
to date return of 27.87 per cent, was the second largest gainer, increasing by 
2.17 per cent. All the major indices ended in the green, with the investment 
index moving up by 2.72 per cent. The Bahraini bourse ended almost flat gaining 
0.41 per cent. However, the insurance and the banking counters gained 7.53 per 
cent and 2.67 per cent, respectively. <br>
<br>
After the heavy buying witness on the Saudi exchange in recent months, the 
general index corrected by 1.21 per cent during the one-month period ended 
September 15. Lacklustre performance was seen in the insurance counters and the 
insurance index dwindled by 10.06 per cent. However, this was partially offset 
by the buying witnessed in the banking counters, which caused the Saudi banking 
index to rise by 1.55 per cent. The Abu Dhabi and Dubai bourses shed 0.67 per 
cent and 0.15 per cent, respectively, during the period. While the industrial 
index lost 3.32 per cent on the Abu Dhabi exchange, banking counters were the 
top losers on the DFM, shedding 1.01 per cent. The much-awaited Deyaar 
Development Company got listed on the DFM bourse on September 5 and generated 
huge volumes in the additional trading sessions.<br>
