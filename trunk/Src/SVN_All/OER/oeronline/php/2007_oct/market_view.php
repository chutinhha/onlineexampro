<b>A lacklustre month on the MSM was otherwise livened by some corporate events. 
OER reports<br>
</b><br>
The Muscat Securities Market displayed lacklustre performance during the 
one-month period ended September 15. The benchmark MSM-30 general index, which 
has generated an 18.36 per cent year to date return, fell 1.07 per cent or 71.92 
points during the period. This was primarily due to selling pressure seen in the 
banking counters, which caused the banking index to drop 1.44 per cent. The 
industrial index augmented by 4.17 per cent and emerged as the highest year to 
date gainer surpassing the banking index and generating a return of 27.91 per 
cent. <br>
<br>
In key corporate events, Galfar Engineering and Contracting Company’s IPO 
mobilised RO872.97 million, indicating an oversubscription of 14.5 times. Galfar 
made an all-time record in IPO collection in the Sultanate, even bigger than the 
funds collected by Oman Telecommunications Company, which offered RO288 million 
and could attract RO700 million. The institutional investors, who will get 40 
per cent of the IPO, offered RO790.72 million while individuals put in RO82.25 
million.<br>
<br>
In another important development, Al Jazeera Steel Products Co.’s board resolved 
that the company is entering into a subscription agreement with Global Buyout 
Fund LP and its associates. The subscription agreement sets out that the company 
will increase its issued share capital from RO6,120,000 to RO12,489,796 by 
issuing 63,697,960 shares, each with a par value of RO0.100 per share and at a 
price of 0.325 per share. The new shares will be allotted to Global Buyout Fund 
LP by way of private placement.<br>
<br>
After a long legal battle, the EGM of Dhofar Cattle Feed Co. finally authorised 
the Board of Directors to take necessary action regarding the withdrawal of 
Garziz Farm and Sahnout Farm legal cases subject to the condition that Ministry 
of Agriculture agree and sign a mutual settlement between the Government and the 
Company.<br>
<br>
Also, Majan Glass Co. has announced the successful commissioning of its fourth 
production line on August 25.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
