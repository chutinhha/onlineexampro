<b>Research Highlights<br>
Raysut Cement Co. SAOG Research Abstract</b><br>
For the first half of 2007, Raysut Cement Co. (RCC) reported an increase of 14.5 
per cent in revenues y-o-y and 4.4 per cent quarter on quarter. While cement 
dispatches reduced during the second quarter, the company was able to maintain a 
positive trend in turnover and profitability due to higher realisations from its 
overseas markets. RCC has been taking advantage of the upward cement cycle by 
adding capacities at the right time. From less than 1 million TPA 2 years ago, 
the company has expanded to 3 million TPA capacity in a phased manner. With 
better realisations and incremental volumes, there has been a marked hike in 
investment return ratios also, driving the price of its stock to current levels. 
With a positive demand outlook and rising realisations, combined with the 
addition of Kiln 4 easing the supply side, we expect RCC to perform well in 
FY07-08, achieving an EPS of RO0.125 and RO0.157 for FY07 and FY08, 
respectively. Vision assigns a ‘Hold’ rating for the stock.</font><p align="justify" style="margin: 3px 10px">
