<img border="0" src="cover5.jpg" width="200" height="235" align="left" hspace="6" vspace="4">Tell us about Action Global Communications.</b><br>
Action Global Communications has set the pace in all the regions in which it 
operates since its establishment in Cyprus in 1971. It has made its name as a 
leading emerging markets specialist agency, operating a unique network. It is 
the largest independent PR network in Central, East and South East Europe, North 
Africa, the Middle East and Central Asia.<br>
<br>
It is a growing network, with global footprint of over 40 countries, with a 
combined population of almost a billion. The company’s reach extends its unique 
brand of reputation management solutions within the emerging markets into three 
continents, Europe, Asia, and Africa; its network of offices closely linked to 
form a wide area of representation for each of its clients, with success built 
on an intelligent in-depth knowledge of cultural factors combined with a strong 
insight into the different modes of conducting business within each country. 
Action Global Communications’ Middle East hub is based in Dubai, with regional 
offices in Qatar, Bahrain, Kuwait and Saudi Arabia. In Oman, we have a 
full-fledged PR agency under the banner of Buzzword-Action.<br>
<br>
<b>What is your outlook for the PR industry in Oman?<br>
</b>The PR industry is a young, yet growing, business in Oman. The term public 
relations in Oman is still misconstrued to be press management, which implies 
solely maintaining media relations, placing press releases and responding to 
media inquiries. <br>
<br>
To date, the opportunities to influence the public opinion forming process is 
nowhere near exploited to its full potential in Oman. Public relations has an 
important role to play with regard to strategic planning, corporate social 
responsibility, internal communications, crisis management and media training as 
well as the overall management of a corporation’s reputation.<br>
<br>
So, the outlook is positive: Oman-based corporations will speedily use and 
utilise public relations to actively manage their reputations. This means growth 
for the consultancy business for agencies like Buzzword-Action and increasing 
job opportunities for in-house positions.<br>
<br>
<b>What are the best PR practices that you bring to Oman?<br>
</b>MCS/Action was founded in 1993 and has an impressive track record of 
consulting major international and local corporations, and institutions in the 
region. Based on that experience, MCS/Action brings exceptional client service 
processes and high consultancy quality to the Omani market. Special industry 
sector expertise includes aviation, automotive, sports, energy/oil and gas, 
finance, travel &amp; leisure. In 2006 and 2007, MCS/Action was presented with an 
award as the best UAE consultancy in the property sector (CNBC Property Award).<br>
<br>
Tailor-made media training workshops for client’s top-management are integral 
parts of major public relations campaigns at MCS/Action. Targeted and successful 
media relations programmes based on well-researched and high-quality English and 
Arabic editorials/press releases are the foundation for successful corporate 
reputation programmes. Crisis communications as well as public affairs 
programmes belong to MCS/Action’s portfolio, which will also be made available 
to Omani clients.</font></p>
