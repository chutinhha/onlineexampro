<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="4"><b>PR Vs Advertising <br>
3 Facts of Life</b></font><font face="Verdana" size="1"><br>
<br>
<b>
<img border="0" src="cover6.jpg" width="200" height="235" align="left" hspace="6" vspace="4">Marketing strategist Al Ries, author of the best-selling ‘The Fall of 
Advertising &amp; The Rise of PR’, speaks to Steve Kayser about when and where PR 
scores over advertising, and other facts of life</b><br>
<br>
<b>You state that today’s major brands are born with publicity, not advertising.<br>
</b>Yes, all recent brand successes have been PR successes, not advertising 
successes. Red Bull, Starbucks, Harry Potter, Linux, Palm, The Body Shop, 
JetBlue, and Google.<br>
<br>
<b>Examples?<br>
</b>Starbucks spent less than US$10 million in advertising in its first 10 years. 
That’s less than one million a year, a trivial amount for a national brand. 
Here’s what Howard Schultz, CEO of Starbucks, has to say about advertising. “It 
is difficult to launch a product through consumer advertising because customers 
don’t really pay attention as they did in the past. I look at the money spent on 
advertising and it surprises me that people still believe they are getting 
returns on their investments.”<br>
<br>
The Body Shop is a worldwide brand that has never advertised. Instead, Anita 
Roddick travelled the world looking for ingredients for her natural cosmetics, 
creating many publicity opportunities. Actually, The Body Shop now needs to do 
what Botox has done – shift from a PR mode to an advertising mode. <br>
<br>
The fastest-growing retail chain in the world is Zara, headquartered in Spain, 
and now operating in 27 countries. As a matter of fact, their tags show the 
price of their merchandise in 27 currencies. Zara does no advertising, except 
for two sale ads a year. JetBlue is flying high, primarily because of PR. The 
October 14, 2002, issue of Forbes, referred to them as “Lord of the Skies.”<br>
<br>
PlayStation and PlayStation 2 were introduced with a fanfare of publicity and 
went on to become the leading video-game brand. <br>
<br>
Microsoft Xbox followed the same pattern. As a matter of fact, 75 per cent of 
the target audience expressed an “interest to buy” before the first Xbox ad ran.
<br>
<br>
Linux has not advertised because no one owns the brand. It is open-source 
software. Yet, Linux has some 99.9 per cent name recognition in the high-tech 
community. <br>
<br>
The Wall Street Journal has become a high-technology trade paper. If you are in 
the high-tech field and your brand is not mentioned favourably and frequently in 
The WSJ, you are not going to make it in the high-tech field. It was publicity 
in The Wall Street Journal and other management publications that built brands 
like Cisco, Dell, Oracle, Microsoft, Palm, SAP, Sun Microsystems and Yahoo.<br>
<br>
<b>But didn’t some major dotcoms succeed with advertising?<br>
</b>Which dotcoms are successful? Amazon, Ebay and other dotcoms relied on PR to 
build their brands. Those that tried to do it with advertising were notable 
failures. Google is another dotcom brand that rode to the top primarily with PR.<br>
<br>
<b>FACT OF LIFE NO. 1:</b><br>
Advertising often gets the credit for PR successes.<br>
<br>
Advertising Age recently ran a special issue on the best campaigns of the 20th 
century. The No. 1 advertising campaign, as you might have guessed, was from 
Volkswagen. The first advertisement in the campaign, “Think small,” was run by 
Doyle Dane Bernbach in 1960. Almost everyone credits this campaign for building 
the Volkswagen brand.<br>
<br>
But in the year before the campaign was launched, Volkswagen was already the 
largest-selling imported car in the country, with 19 per cent share, primarily 
due to favourable publicity. Granted, the DDB campaign accelerated Volkswagen’s 
sales, which is exactly what the best advertising should do.<br>
<br>
The best single advertisement of the 20th century, according to many 
commentators, was a Rolls-Royce ad. “At 60 miles per hour, the loudest noise in 
the new Rolls-Royce comes from the electric clock.”<br>
<br>
David Ogilvy said: “The best headline I ever wrote contained 18 words: “At 60 
miles per hour, the loudest noise in the new Rolls-Royce comes from the electric 
clock.”<br>
<br>
Have you read the first paragraph of the ad? I’ll read it for you. “At 60 miles 
per hour, the loudest noise comes from the electric clock,” reports the 
Technical Editor of The Motor, the leading automotive publication in the United 
Kingdom.<br>
<br>
David Ogilvy took his headline directly from a road test in a motor magazine. Do 
I think any less of Ogilvy’s genius? Of course, not. That’s what advertising 
ought to do. Pick up and reinforce ideas put into the mind by PR.<br>
<br>
<b>FACT OF LIFE NO. 2:<br>
Advertising often gets the credit for campaigns that don’t deserve it.<br>
</b>Take the Energizer Bunny, one of the most admired advertising campaigns of all 
time. Is Energizer the leading appliance battery brand? Of course, not. The 
leading appliance battery brand is Duracell by a big margin. Recently, 
MasterCard’s “Priceless” campaign has gotten a lot of publicity. Terrific, but 
Visa leads MasterCard by more than two to one.<br>
<br>
<b>FACT OF LIFE NO. 3:<br>
Advertising dollars cannot compensate for the lack of favourable PR.</b><br>
No-brainer. The largest advertised brand in America spent US$780 million on 
advertising last year. Do you know the name of the largest advertised brand? 
It’s not McDonald’s, Budweiser or Coca-Cola. <br>
<br>
The largest advertised brand, would you believe, was Chevrolet. Now let me 
ask you a question, what’s a Chevrolet? If I told you I would meet you out front 
in my Chevrolet, would you be able to recognise my car?<br>
<br>
What’s a Chevrolet? A large, small, cheap, expensive car… or truck. But you 
already knew that. US$780 million spent and there probably isn’t one thing stuck 
in your mind that you can connect with Chevrolet. What a waste.<br>
<br>
The largest corporate advertiser in America last year was Chevrolet’s parent, 
General Motors. As a matter of fact, the company has been the largest corporate 
advertiser for five of the last eight years. <br>
<br>
In eight years, General Motors has spent US$23 billion on advertising. What did 
they get for their money? They lost 7 per cent of market share, that’s what they 
got – from 34 per cent in 1995 to 28 per cent in 2001.<br>
<br>
Big advertisers often are companies with big problems. Advertising can often 
accelerate success, but it usually does nothing to forestall failure.<br>
<br>
When US Airways went bankrupt, for example, what was the first thing they did? 
They ran full-page advertisements signed by the chief executive in The Wall 
Street Journal, The New York Times and USA Today. “Foundation for the future.”<br>
<br>
When United Airlines went bankrupt, what did they do? They ran full-page 
advertisements in The Wall Street Journal, The New York Times and USA Today. 
“It’s not really Chapter 11, it’s Chapter 1.”<br>
<br>
When Firestone got into trouble, it ran full-page advertisements signed by its 
chief executive in The Wall Street Journal, The New York Times and USA Today. 
“Making it right.” Translation: We have been making our tyres wrong for 50 
years, now we are going to start making our tyres right.<br>
<br>
When Arthur Andersen got into trouble, it ran full-page advertisements signed by 
the managing partner, the ex-managing partner, in The Wall Street Journal, The 
New York Times and USA Today.<br>
<br>
But when Merrill Lynch got into trouble, big trouble, it didn’t run full-page 
advertisements in The Wall Street Journal, The New York Times and USA Today. It 
ran two-page spreads in those publications signed by both the CEO and the 
president. “Lately, you’ve been hearing a lot about Merrill Lynch.” Now we are 
going to set you straight. And what two guys in US$2,000 suits tell you, you 
know you can believe.”<br>
<br>
<b>PR vs. Advertising… biggest takeaway?</b><br>
Advertising’s Achilles’ heel is not a heel at all. It’s the mind of the 
prospect. Advertising has little credibility in the mind.<br>
<br>
Enamelon, a toothpaste that adds enamel to your teeth, spent US$25 million 
launching the brand and received US$10 million in sales. Adds enamel to your 
teeth? A product like this needs to start with a PR programme in publications 
like The Journal of the American Dental Association. <br>
<br>
Advertising is self-serving. What you say about yourself has little or no 
credibility in the mind.<br>
<br>
“I did not have sexual relations with that woman, Miss Lewinsky,” said Bill 
Clinton. Did you believe that? Did Hillary believe that? <br>
<br>
“I will not resign,” said Richard Nixon and then promptly resigned. PR has 
credibility in the mind. It’s the third-party effect.<br>
<br>
It was PR that built the safety position for Volvo. And advertising reinforced 
it. It’s what we call PR-oriented advertising. Use PR first to establish the 
credibility of the brand, and advertising second to reaffirm and reinforce the 
brand’s credibility. <br>
<br>
The Volvo ad works because “safety and Volvo” are synonymous in the mind. “We 
design every Volvo to look like this.” But this ad doesn’t work. “We design 
every Dodge to look like this?” The steering of a Dodge must be defective 
because look at all the accidents they have been having.<br>
<br>
Advertising agencies, as you know, generally ignore the credibility issue and 
focus on creativity. Take the sock puppet owned by Pets.com. The sock puppet 
received US$60 million in advertising yet delivered only US$22 million in sales.
<br>
<br>
As it happens so often in advertising, creativity dies again. <br>
<br>
As strange as it might seem, the ad agency disagrees. Here is what the world’s 
most famous creative director said about the Pets.com campaign. “Business 
models, market conditions, the Nasdaq, VCs – they’re not in my control. This has 
nothing to do with the success of the advertising. Ad agencies are hired to 
create brands, and we did that in spades.”<br>
<br>
It’s the classic advertising error. The ad agency apparently thinks the brand is 
the sock puppet. But consumers don’t buy sock puppets. Consumers buy pet 
supplies. And few did because the advertising didn’t build the Pets.com brand.
