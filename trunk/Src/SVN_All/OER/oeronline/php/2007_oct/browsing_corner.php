        entrepreneurs. In today’s hedonistic world, there are so many of us who 
        feel mired and ill-fitted into roles that we are acting out, not because 
        we want to, but because we have to. The scary fact is that we utilise 
        our strengths to our optimum only about once a week. If this sounds like 
        the story of your life too, then dip into Marcus Buckindgham’s book, ‘Go 
        Put Your Strengths To Work,’ and ladle out dollops of wisdom as you 
        learn how to capitalise on them.<br>
        <br>
        So, what exactly is this strengths’ movement? It’s a journey of 
        recognising and knowing yourself through self-internalisation, and then 
        moving quickly towards disciplining yourself into action. From 
        observation to action, therein lies a six-step journey. Tread each step 
        carefully as you bridge this gap. Who says it’s going to be easy? I 
        didn’t! And neither does the author. But perseverance pays, as all of us 
        know only too well. The paradigm shift is to learn from our successes 
        vis-à-vis the conventional wisdom of learning from our mistakes. Because 
        all we learn from our mistakes are the characteristics of mistakes. Now 
        that’s definitely something to think about!<br>
        <br>
        Time then to hit the first step, which is to bust the myths. You’ll read 
        about these myths, taking you closer to yourself and giving you a deeper 
        realisation of your likes and dislikes. The second step familiarises you 
        with those moments of realisation and gives you greater clarity on what 
        your strengths really are. Then sift your strengths and weakness using 
        the author’s ‘Strengths Engagement Track or SET’. As you progress, you 
        learn where your weaknesses stem from and how to nip them in the bud. 
        Finally, you discover how to address your strengths and make them 
        winning habits.<br>
        <br>
        Excel in whatever you take up by using the key learning points. A bigger 
        paycheck, that desired promotion, more perks…all this and more can be 
        yours by exploiting your strengths. Write down your strength and 
        weakness statements, work out ‘Strong Week Plans’, FREE (focus, release, 
        educate, expand) your mind and pick up handy tips along the way. Decide 
        where the best from you can be expected and what kinds of activities you 
        should steer clear of because they don’t make you comfortable. Build 
        teams using the strengths’ movement by sitting down as a group, 
        determining project objectives, activity plans and outlining 
        responsibilities based on individual expertise.<br>
        <br>
        Figure out what’s right for you by capturing what excites and what 
        drains you. Focus on what’s interesting. Identify how each strength 
        helps you in your current role. Identify the missed opportunities and 
        leverage your skills. Let the enthusiasm flow. More power to you! <br>
