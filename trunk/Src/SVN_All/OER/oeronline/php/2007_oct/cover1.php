</font><b><font face="Verdana" size="1">ASDA’A PUBLIC RELATIONS<br>
<br>
</font><font face="Verdana" size="4">‘Develop the market’</font></b><font face="Verdana" size="1"><br>
<b>Sunil John, Partner &amp; Managing Director – Middle East, ASDA’A Public 
Relations, talks about the transformation taking place in PR and the Oman market 
per se</b><br>
<br>
<b>
<img border="0" src="cove5.jpg" width="200" height="235" align="left" hspace="6" vspace="4">What is the size of public relations industry in Oman and how will it change 
in the coming years? <br>
</b>The public relations industry in Oman is in a development stage but is 
growing steadily: we predict annual growth of 20 to 30 per cent, which is a very 
positive indicator. The regional market for public relations is valued at US$60 
million, with industry estimates suggesting that it could approach US$100 
million in 2007, and Oman has a small but significant portion of that sector.
<br>
<br>
Oman also has considerable activity in real estate, finance, tourism and health, 
some of the key growth sectors for PR in the region. ASDA’A opened a 
full-fledged office in Muscat in September 2006 acknowledging the growing need 
and potential in Oman. In a short time, our Oman office has been able to engage 
with blue-chip companies, including Bank Muscat, The Wave and Octal Holdings.<br>
<br>
In addition, we are very active in the Oman market on behalf of our regional 
retainer clients such as DHL, Starbucks, Welcare World Health Systems, GE, 
Swiss, Ford and Giordano.<br>
<br>
<b>With developments such as the growing popularity of the Internet and constant 
media fragmentation taking place in the Gulf, how is PR transforming itself to 
meet the new challenges and emerging opportunities? <br>
</b>The key lesson of the new media landscape is that the old vertical, top-down 
method of communication no longer works. Today, horizontal communications, which 
operates in the peer-to-peer space on the internet and word of mouth channels, 
has taken a much larger and impactful role. PR is now focussing on the sweet 
spot between both the traditional vertical model and the horizontal axis to 
bring better results for our clients. Also trust in businesses, in CEOs and in 
governments is eroding. According to the Edelman Trust Barometer, a key piece of 
international research that has been widely cited around the world, the most 
credible source of information is now ‘a person like me.’ <br>
<br>
We are far more likely to trust our friends, colleagues and others we know, and 
accept their recommendations and advice, and change our behaviour as consumers 
accordingly. This is revolutionary stuff: the early days where CEOs and big 
businesses were trusted and believed implicitly in are gone. For the PR 
industry, this is a godsend, because PR is uniquely qualified – and uniquely 
capable – of building and delivering that trust. Trust is the currency of PR.<br>
<br>
For a business to survive and weather crises today – even reputational crises 
that can be triggered by a single blogger with a computer – its messages must be 
on target, its communications nimble, responsive and quick thinking. Reputations 
can be made or broken within hours. PR is the only communications discipline 
with the ability to bridge the divide between a fragmented media landscape and 
increasingly tuned out consumers.</font></p>
