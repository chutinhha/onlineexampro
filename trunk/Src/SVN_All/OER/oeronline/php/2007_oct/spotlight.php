</font><b><font face="Verdana" size="4">Go Hi Fi!</font><font face="Verdana" size="2"><br>
</font></b><font face="Verdana" size="1"><b>The entertainment electronic product market in Oman is going through a rapid 
growth phase, with price levels being at par with those in neighbouring 
countries<br>
</b><br>
<img border="0" src="spotlight.jpg" width="350" height="207" align="left" hspace="6" vspace="4">Planning to shop for the best quality home electronics products, for the 
ultimate in home entertainment? The perfect time to buy them is now as the 
market is awash with a wide range of top-quality DVD/VCD players, hi-fi, 
multifunction audio systems, digicams or latest mobile phones, etc.; all waiting 
for you in the stores.<br>
<br>
There is clearly a burgeoning consumer demand for entertainment electronic 
products in Oman today and all the major players vouch for that. As P V Rao, 
Senior DGM, Genetco, says, “The digital electronics segment has grown over 400 
per cent during the past year.” The booming demand for entertainment electronics 
has also been accompanied by special promotion offers from retailers translating 
into a windfall for customers. Devan Thuppali, GM, OHI Electronics, one of the 
leading retailers of electronics products says, “The growth of entertainment 
electronic products in Oman market has been amazing over the last few years and 
under the wise leadership of HM Sultan Qaboos we’re seeing the advent of many 
large projects in industry and tourism. This has directly contributed to an 
amazing growth in this sector in Oman.” <br>
<br>
<b>MAJOR RETAILERS<br>
</b>The number of dealers in entertainment electronic products is growing by the 
day in Oman, thanks to the demand for these products that’s going over the roof. 
Among the major retailers is OHI Electronics, that is the exclusive distributors 
for JVC, Bose, NEC and Nikai products in Oman. As part of its special offers to 
encourage buying of entertainment electronic products, OHI celebrating its 30th 
anniversary, is currently running a Ramadan promotion and is giving away a home 
maker bundled offer for every RO30 spent in ITS showrooms. What’s more, 30 lucky 
winners will take home a complete set of JVC 29-inch TVs, JVC home theatre 
systems, microwave ovens, juicer and blenders and toasters from Nikai.<br>
<br>
Another major retailer in the entertainment electronic products is Genetco that 
deals in plasma/LCD TVs, MP3/MP4 players, digital cameras, DVD recorders, 
high-end home theatre systems, pro-audio systems, musical instruments, and so 
on, from a host of international brands like Sanyo, Yamaha, Canon, Iriver, 
Videocon, etc. Genetco is also offering easy finance schemes and special 
seasonal offers to trigger buying.<br>
<br>
Oman Marketing &amp; Services Company is another big-wig retailer of entertainment 
electronic products dealing in Panasonic brand, which has a wide variety of 
entertainment products, especially HDTV (both plasma and LCD under Viera name), 
digital still cameras (Lumix range), handy cams (E cam range for SD Camera, high 
definition camera, hard disk camera, DVD camera, DV camera), DVD recorders, DVD 
players, home theatres, Hi-Fis, radio cassette recorders (RCRs) like 
two-in–ones, etc.<br>
<br>
As part of special offers, George Alexander, Divisional Manager, (Consumer 
Electronics Division), Oman Marketing &amp; Services Company, says, “We regularly 
run advertisements to educate the customer on new product arrivals and the 
technology and feature advancement.”<br>
<br>
Emax, a division of Dubai-based Landmark Group, has the largest electronic store 
in Oman at Muscat City Centre, spread over an expansive 25,000 sq ft. Apart from 
Oman, Emax stores are also present in UAE, Saudi Arabia, Qatar and Bahrain. As 
the largest retailer of electronic product, Emax deals in a galaxy of products 
ranging from mobile phones, portable MP3/ MP4 players, iPods, portable DVD 
players, Hi-Fis, digital cameras, handy cams, DVD recorders and players, 
HD-ready LCD, plasma and projection TVs, sound systems, home theatres, video 
games, gaming consoles including Playstations, X Box, Nintendo WII, musical 
CDs/DVDs, cassettes as well as whole range of old and new English, Hindi and 
Arabic movie CD/ DVDs, satellite receivers, car stereos, audio video recorders, 
digital photo frames. <br>
<br>
Recently, Emax has launched a slew of promotional offers. As Vineet Mehrotra, 
Concept Head, Emax, Oman, says, “Emax has bundle offers wherein one product 
comes free with the purchase of another. Secondly, there are offers which are 
brand specific. Thirdly, there are power retailer specific like Emax exclusive 
deals wherein certain brands offer exclusive deals for products being sold only 
at Emax.” Then there is Al-Seeb Technical Est. (SARCO) that deals in LCDs, 
plasma, and CRT based TVs, Hi Fi music systems, MP3 players, HD DVD Players, 
camcorders and home theatre systems. As part of its Ramadan sales promotion 
effort, SARCO is offering ‘Triple Bonanza Offer’ as per which customers will 
benefit from triple benefits of Scratch-and-win opportunity for every purchase 
of RO 25 of Samsung products. Apart from this, they would also get fabulous 
discounts on Samsung products and extended warranties on their purchase during 
the offer.<br>
<br>
<b>CATCHING UP<br>
</b>The UAE continues to be the hub for all new entertainment electronic product 
introductions. However, with the recent increase in the cost of operations in 
Dubai, high-end electronic products are no longer cheaper there. As P V Rao, 
Senior DGM, Genetco, says, “The gap in prices between Oman and UAE is almost 
bridged. Now most customers for costly high-end equipment prefer to buy from 
local agents to avail the after-sales support, etc.”<br>
<br>
Vineet Mehrotra of Emax also agrees. “Trends are changing and forcing customers 
to change their opinion that UAE is a cheaper shopping destination. Almost all 
leading international brands are available in Oman and most of them have adopted 
pricing strategies in line with UAE pricing. As a result, consumers do not want 
to take the trouble of going all the way to Dubai/UAE. Secondly, the after-sales 
service compulsions also force consumers to buy products from local distributors 
of the concerned brands.”<br>
<br>
Ajay Ganti, GM, SARCO says, “The perception still is that UAE offers better 
products and pricing but gradually the perception is changing with more and more 
top- notch shopping malls on the rise and the existing ones upgrading themselves 
to world-class standards. Adding to this, the tendency of both the Omani 
nationals and increasing expatriate population’s allocation of disposable income 
towards entertainment electronics among others is increasing. Oman will carve 
its own niche in the times to come.”<br>
<br>
<b>CHALLENGES<br>
</b>However, despite the astounding growth, the burgeoning entertainment 
electronic product market is facing certain challenges. As Devan Thuppali of OHI 
Electronics says, “The challenge is to provide customers with a complete 
solution in terms of proper installation and most importantly impart them with 
sufficient product training to enable them to use all the features of the 
product purchased to make them get value for money. Secondly, with the influx of 
many Chinese brands with ridiculously low prices, educating customers with 
cost-benefit analysis is a major task.”<br>
<br>
George Alexander of Oman Marketing &amp; Services Company says, “Although the demand 
for entertainment electronics products is picking up, the penetration levels of 
these products are low in comparison to home appliance products. The rate of 
growth is extremely high in HDTV, and digital still cameras, but the RCR and Hi-Fi 
range is slowing down. Further, constant change in technology and educating the 
customer about the same is the biggest challenge.”<br>
<br>
Vineet Mehrotra enumerates the three biggest challenges for the entertainment 
electronics product market in Oman – tapping the high income customer segment; 
tapping the floating customer population; and, attracting more media attention 
and creating a need and awareness.”<br>
