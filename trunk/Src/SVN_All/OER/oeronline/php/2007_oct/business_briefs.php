<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font color="#CC3300" size="2" face="Verdana"><b>Oman among top 20 in 
economic freedom rankings</b></font><font face="Verdana" size="1"><br>
<img border="0" src="business9.jpg" width="250" height="168" align="right" hspace="6" vspace="4">Oman has entered the top 20 in the global world economic freedom rankings, 
moving from 24 last year to 18th spot this year, according to the ‘Economic 
Freedom of the World: 2007 Annual Report’ released recently by the International 
Research Foundation (IRF). The Sultanate has scored in key components of 
economic freedom measurements on a scale of 1-to-10 where a higher value 
indicates a higher level of freedom. The score for size of government changed 
from 5.9 in last year’s report to 5.7; legal structures and security of property 
rights changed from 7.5 to 7.7; access to sound money changed from 8.6 to 9.4; 
freedom to trade internationally changed from 7.8 to 7.3; while regulation of 
credit, labour and business went up from 6.6 to 8. The Sultanate is essentially 
tied with the UAE for the regional lead but is well above the global average on 
all counts, disclosed Fred McMahon, director of Centre for Globalisation Studies 
of the Fraser Institute. The Canada-based Fraser Institute prepared the report 
in cooperation with independent research organisations in 71 countries, 
including the IRF in Oman. Hong Kong once again tops the international rankings 
for economic freedom, with Singapore a close second and New Zealand third. 
Zimbabwe and Myanmar had the lowest economic freedom ratings of the 141 
countries measured.<br>
