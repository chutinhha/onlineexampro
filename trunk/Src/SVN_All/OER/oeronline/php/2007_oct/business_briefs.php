invaluable opportunity for young Omani entrepreneurs. “Qumreiyat has attracted a 
significant amount of interest since its launch and taking up residency in the 
TKM incubator program at KOM. The competition provided the focus the team needed 
to define its business strategy. The document we had to produce was a valuable 
starting point on which to build the broader plan for the launch. Our appearance 
at last year’s final clearly raised the company’s profile to a national level, 
something we could not have achieved so quickly on our own.” <br>
