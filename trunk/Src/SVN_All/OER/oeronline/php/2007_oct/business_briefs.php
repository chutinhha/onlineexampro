</font><font color="#CC3300" size="2" face="Verdana"><b>US$660-mn loan for Al-Duqm 
Port and Dry Dock</b></font><font face="Verdana" size="1"><br>
<img border="0" src="business6.jpg" align="left" hspace="6" vspace="4">The Japan Bank for International Cooperation will give a loan of US$660 million 
(about RO254 million) towards financing the Al-Duqm Port and Dry Dock project 
whose total investment cost is about US$1,096 million (about RO421 million). The 
period of the loan extends to 14 years. The project aims at constructing a port 
and a dry dock in the wilayat of Al-Duqm in the Al-Wusta region to meet the 
requirements of the international market to use the quay facilities to satisfy 
projected industry needs coming up as part of the comprehensive plan to develop 
the wilayat of Al-Duqm.<br>
<br>
A comprehensive master plan has been prepared for Duqm by the Supreme Committee 
for Town Planning, Sultanate of Oman. The area will have a major dry dock, 
commercial port and a shipbuilding yard in future. The master plan has 
designated areas for airport, petroleum refinery and oil storage facilities 
along with downstream industries, a fishing harbour with fishery related 
industries, free trade zones, general industries and tourism projects. It has 
planned a complete residential town with a Town Centre and all modern amenities. 
A tentative allocation of 1,026 hectares has been made and designated as the 
Free Trade Zone. However, the total area in five different locations within the 
Duqm development area available both for FTZ and general industries is about 
21,000 hectares. Out of this, about 3,300 hectares has been designated for the 
study of setting Duqm refinery and petrochemical complex, and about 1,300 
hectares for fishery related industries. The commercial and dry dock facility 
would come up on 237 hectares. While the main port area would come up on a site 
of 115.6 hectares, ship repair facility would have 102-hectare area. It also 
will have about 13.4 hectares for workers and shipping clients to stay and rest. 
Exploring opportunities in tourism is one of the main components in the master 
plan, which includes hotels, resorts and a public beach.<br>
