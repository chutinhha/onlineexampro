<br>
</font><font color="#CC3300" size="2" face="Verdana"><b>New Zealand expertise to 
assist Omani fisheries</b></font><font face="Verdana" size="1"><br>
The Oman Government has awarded a major US$ 7 million contract to assess the 
country’s fish stocks to a New Zealand consortium spearheaded by fisheries 
management consultant, Bruce Shallard and Associates, according to a press 
statement issued by Bruce Shallard and Associates. It will be the first fish 
stock survey undertaken in Oman since the 1980s. Shallard and his New Zealand 
consortium partners NIWA (National Institute of Water &amp; Atmospheric Research), 
Independent Fisheries and Deloitte will spend two years surveying and analysing 
Oman’s fish stocks to help the government make strategic decisions based on 
sound scientific information about the future development of Oman’s fishing 
industry. The New Zealand scientists will be conducting surveys using 
sophisticated technology and a large New Zealand trawler, renamed “Al Mustaqilla” 
(Arabic for independent).<br>
<br>
With the assistance of New Zealand Trade and Enterprise (NZTE), the country’s 
economic development agency, Shallard has spent many years developing 
relationships in the Middle East including completing contracts in Iran and the 
United Arab Emirates. New Zealand’s reputation for world leading fisheries 
management is seen as a huge asset to looking at the ongoing management of Omani 
fish stocks.<br>
