firm would team up with Iranian and Turkish companies, since the three are 
leading Islamic countries in the auto-manufacturing field. “We have taken the 
approval of the Malaysian government in this respect and the country’s officials 
can confer with the Iranian government on manufacturing the Islamic cars,” he 
suggested <br>
