into one unit. Visa combined its businesses, excluding Europe, to save money, 
facilitate the share sale, streamline decision-making and make acquisitions 
easier, the company said in its filing. Net income for the combined company was 
$770.9 million for the nine months ended June 30. Visa had revenues of $3.73 
