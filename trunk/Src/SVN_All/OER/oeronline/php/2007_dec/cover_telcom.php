<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="2" color="#CC3300">
Telecom</font></b><font face="Verdana" size="1"><br>
<br>
</font><b><font face="Verdana" size="4">
100 PER CENT PENETRATION</font></b><font face="Verdana" size="1"><br>
<img border="0" src="cover10.jpg" width="175" height="475" align="left" hspace="6" vspace="4">In recent years, the Sultanate of Oman has achieved tremendous success in 
catching up with the global telecommunications advancement in terms of 
penetration as well as providing a world-class service to its consumers. The 
government’s encouragement to the sector by opening it up for private players 
and providing excellent infrastructure has certainly brought laurels. Today, 
Oman has emerged as one of the fastest growing telecom markets in the region.<br>
<br>
Overall telecom penetration in Oman went up dramatically in 2007. The total 
number of mobile subscribers rose to 2.3 million as on September 2007. And if we 
include the 259,281 fixed line subscribers, the total number of subscribers 
touches almost 2.6 million – almost 100 per cent penetration! (source: Ministry 
of National Economy).<br>
<br>
As we look back on 2007, we find that Nawras has managed to attract a large 
number of subscribers with its aggressive marketing. This is even as Oman Mobile 
has managed to maintain its lead by coming up with consumer friendly offers. 
However, the most prominent newsmaker in the telecom market this year has been 
Omantel. <br>
<br>
To quote an OBG report, ‘a further tranche of the government’s stake in market 
leader Omantel will be privatised, its structure overhauled and royalties to the 
government cut as both Omantel and its rival Nawras look to boost revenues by 
moving into less explored market niches. <br>
<br>
‘The proposed royalty cuts would alone add around US$36.37 million to Omantel’s 
profits in 2007. <br>
<br>
‘The royalty rates paid to the government were cut to 7 per cent from 10 per 
cent of revenue for fixed line services and to 7 per cent from 12 per cent from 
mobile services, under its daughter company Oman Mobile. It is not expected that 
the government will suffer significant revenue losses, as the cuts in royalty 
requirements will be fed through to the end consumer, increasing revenues.<br>
<br>
‘Omantel’s shares surged 9.99 per cent to hit a 16 month high after the 
government’s announcement. The increase is the maximum permitted in a single 
trading day. The stock had tumbled 26 per cent this year.<br>
<br>
‘The Omani government announced in October that it would be selling part of its 
70 per cent stake in Omantel, which includes both the country’s eponymous sole 
fixed line operator and its leading mobile company, Oman Mobile. The state had 
previously sold off 30 per cent in 2005 through an initial public offering, 
which raised US$729.5 million. Under the firm’s current statutes, a maximum of a 
further 19 per cent can be sold off by the state. However, with shareholder 
approval, this cap can be lifted and a greater proportion privatised. The cash 
raised by Omantel’s public offering should help support its moves to acquire 
firms abroad, a key part of its diversification strategy.<br>
<br>
<img border="0" src="cover12.jpg" width="250" height="187" align="right" hspace="6" vspace="4">‘It is hoped that private investors will help provide the capital and expertise 
needed for Omantel to develop operations outside Oman. Analysts have said that a 
major regional player, or even one from the Indian subcontinent–could become a 
strategic investor, taking advantage of synergies and economies of scale in its 
operations.<br>
<br>
‘The firm lost its mobile monopoly in 2005 and has lost 40 per cent of its 
domestic market share, and its fixed line monopoly will be rescinded at the end 
of this year. These changes make developing operations abroad central to 
securing future growth. Having said that, the stellar growth of the Omani 
telecom market has meant that it is still taking on new customers at a steady 
clip. <br>
<br>
‘Oman Mobile’s US$20.5 million deal with Siemens Communication to boost GSM 
coverage in the Sharqiya, Dakhliya, Dharia and Wusta regions should help it 
improve services for existing customers and reach new ones in remote areas. 
There is also scope for boosting revenues from shifting users from pre-pay to 
post-pay subscriptions. As of last year, 85 per cent of mobile telephones in 
Oman were operated on a pre-pay basis. Post-pay contracts move customers up the 
value chain, creating greater customer loyalty and the opportunity to offer more 
products and services to the client.<br>
<br>
‘In the fixed line segment of the business, there have been some problems on the 
supply side, with lines taking up to three months to install. Thus, fixed line 
growth has been relatively low compared to the galloping rate of mobile take 
up.’<br>
<br>
Omantel is already actively seeking to invest outside the Sultanate. According 
to Omantel, the value of its offer to acquire a majority stake of 65 per cent in 
Pakistan World Call Telecom Company (WCTC) is around US$204 million. It would 
purchase 60 per cent of the targeted stake from the leading owners of the 
company and 5 per cent from other shareholders through the securities market.<br>
<br>
<img border="0" src="cover11.jpg" width="225" height="292" align="left" hspace="6" vspace="4">As per OBG, ‘Nawras had a 27 per cent market share after its first 18 months of 
operating. The company has invested $140 million in infrastructure development, 
with $1.5 billion in the pipeline over the next eight years.<br>
<br>
‘Nawras will need these investments in order to build its network coverage, 
which currently lags somewhat behind the 95 per cent of population covered by 
Oman Mobile – though company officials proudly report that the network 
experienced no outages during the run up to the holy month of Ramadan and its 
concluding festival, Eid, when phone use is particularly high. <br>
<br>
‘Like Oman Mobile, Nawras is of course looking to boost revenues by extending 
their services to those residents who are yet to obtain a mobile, as well as 
offering more high tech products to the upwardly mobile. Nawras won Oman’s first 
3G licence at the same time as the contract to become the country’s second 
mobile operator, and started to roll out the service in selected cities at the 
beginning of last year.<br>
<br>
‘Oman’s telecom sector gives ample scope for expansion for both Omantel and 
Nawras. Omantel’s part-privatisation should provide capital and greater 
autonomy, and the cut in royalty payments an immediate boost in revenues that 
the firm needs to expand at home and abroad.</font></p>
