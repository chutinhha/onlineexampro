</font><font color="#CC3300" size="2" face="Verdana"><b>Oman Oil, Sohar 
Aluminium in RO8.7-m deal</b></font><font size="1" face="Verdana"><br>
<img border="0" src="bus1.jpg" align="left" hspace="6" vspace="4">Oman Oil Marketing Company (OOMCO) has signed a 10-year diesel fuel agreement 
with Sohar Aluminium. The pact is for the operation of all vehicles on the Sohar 
Aluminium site, at the main port and as back up fuel for the turbines at its 
power station in the event that there is a gas shortage. The contract is valued 
at RO8.7 million. Sohar Aluminium is expected to be in the range of 4 million 
litres per year approximately.<br>
<br>
“This is the beginning of a thriving partnership between Oman Oil Marketing 
Company and Sohar Aluminium,” said Eng. Omar Ahmed Qatan, Oman Oil Marketing 
Company CEO. “As strategic partners, we will provide expertise in our field to 
build synergy to make this project a successful world-class facility and 
contribute positively to the economic growth of the Sultanate. By understanding 
Sohar Aluminium’s needs, we are able to provide efficient and effective 
solutions to their fuel requirements with unsurpassed quality of service.”<br>
<br>
Tony Kinsman, Sohar Aluminium CEO, added, “Sohar Aluminium will be the world’s 
first smelter to pioneer the use of Alcan’s AP35 technology which is considered 
to be the most efficient and environmentally friendly smelter technology ever 
with an annual production of 350,000 tonnes of liquid metal. Therefore, this 
agreement with Oman Oil Marketing Company is of vital importance to the 
provision of mass fuel supplies for achieving this objective.”<br>
