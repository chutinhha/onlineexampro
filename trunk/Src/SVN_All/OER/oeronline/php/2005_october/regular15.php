            inflation and hurt consumer confidence. The lender increased its 
            forecast for oil prices -- an annual average of UK Brent, Dubai, and 
            West Texas Intermediate spot prices - to $54.23 for 2005 and $61.75 
            in 2006 from its previous estimates of $46.50 and $43.75.<br>
