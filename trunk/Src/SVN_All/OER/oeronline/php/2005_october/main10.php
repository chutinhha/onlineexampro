<p align="justify" dir="ltr"><font face="Verdana" size="1">Many managers we have 
worked with have the characteristics of Manager A. Indeed, our Manager A clients 
often tell us that their job is to make optimal decisions by considering all the 
critical data in an orderly and logical fashion. After all, managers (and 
leaders) are charged with making good decisions. However, making good decisions 
and being an effective manager of self and others cannot – and does not – happen 
in the absence of emotion. Emotions are at work, and they work with and for us.
<br>
<br>
<b>Do emotions in the workplace matter?</b><br>
Scientists have learned a lot about the role of emotions in the workplace by 
conducting thousands of research studies. Some of the results of these studies 
may surprise you. For instance, how managers feel is a useful indicator and 
predictor of organizational performance. In fact, research by Sigal Barsade, a 
professor at the Wharton School of the University of Pennsylvania, demonstrates 
that how a management team feels has a direct impact on a company’s earnings. 
She discovered that a top management team that shares a common, emotional 
outlook that is positive will have 4 to 6 per cent higher market-adjusted 
earnings per share than companies whose management team consists of members with 
diverse emotional outlook.<br>
<img border="0" src="foodfh2.jpg" width="76" height="89" align="right"><br>
In a nine-week-long research study by University of Queensland’s Peter Jordan 
and Neil Ashkanasy, teams consisting of members low in emotional intelligence 
ended up at the same level as did teams of people with high EI. At first blush, 
these results are not something that the high-EI manager might expect. What is 
striking is the difference in performance during the first weeks of the study. 
The high-EI teams were able to get their act in gear a whole lot faster than the 
low-EI teams. Eventually, the low-EI teams did catch up to their more 
emotionally intelligent peers. The lesson learned from this study is that team 
emotional intelligence doesn’t much matter – as long as you don’t mind weeks of 
lost team productivity and hundreds of worker hours wasted. <br>
<br>
<img border="0" src="foodfh3.jpg" width="76" height="89" align="left">Emotions 
at a team level have a powerful impact in other ways as well. You might call it 
team spirit or morale, but all of us have experienced how the mood of a group 
can change. And how we feel does seem to influence our performance. Sometimes it 
happens slowly and subtly, but sometimes you can almost feel a chill come over 
the group; at other times, a sense of excitement permeates the air. The spread 
of emotions from person to person is a phenomenon known as emotional contagion.
<br>
Emotional contagion has powerful effects on a group. Consider the experiment in 
which several groups of people were asked to stimulate an end-of-the-year bonus 
pool discussion. Their role was to get as large a bonus for their employees as 
possible, while still attempting to make the best decisions for the organization 
as a whole. One of the people in the group, unbeknownst to the discussants, was 
trained actor who behaved in a negative manner with some of the groups and in a 
positive manner with the other groups. Videos of the groups made it clear that 
the actor had an impact on the group’s mood, depressing it in the negative 
condition and enhancing it in the positive condition. The research participants 
also reported changes in their mood, but they did not seem to realize why their 
mood had changed. Even more important, the positive groups showed a lot less 
conflict and much more cooperation than did their negative-mood counterparts.<br>
<br>
But emotional contagion, on its own, is neither intelligent nor unintelligent. 
The strategic application of emotional contagion is what makes it part of the 
repertoire of the emotionally intelligent manager.<br>
How leaders feel also affects how, and how well, they influence people, which, 
after all, is the core of leadership. A leader who is feeling sad is more likely 
to generate arguments that are persuasive and well thought out. Sad moods, in 
general, help people think in a more bottom-up, systematic manner than do happy 
moods. The same leader, feeling somewhat happy, will probably generate creative 
and original arguments in a bid to influence others. This leader will also come 
up with a whole lot more arguments when feeling happy than when feeling sad. And 
in general, emotions at work influence judgement, job satisfaction, helping 
behaviour, creative problem solving, and decision-making.<br>
<br>
What makes all of this either smart or dumb is whether you realize the role that 
emotions play and what you do with that knowledge. Do you try to get your team 
to generate creative messages when they are down in the dumps? Or do you use 
this time instead to critically evaluate and edit a prospectus? The emotionally 
intelligent manager matches the mood to the moment.<br>
<br>
We don’t expect most managers to know how to do this. You might have taken 
courses in accounting and marketing, but we’ll bet that you never took a course 
on emotion management strategies, emotional identification skills, or emotion 
generation. So consider this book your course on emotions at work – why they 
matter, how they operate, and how to leverage the power of your emotions to be a 
better manager and leader.<br>
Your emotional education starts with the six basic principles of emotional 
intelligence. <br>
<br>
<b>Six principles of emotional intelligence</b><br>
1 Emotion is information.<br>
2 We can try to ignore emotion, but it doesn’t work.<br>
3 We can try to hide emotions, but we are not as good at it as we think.<br>
4 Decisions must incorporate emotion to be effective.<br>
5 Emotions follow logical patterns.<br>
6 Emotional universals exist, but so do specifics. <br>
