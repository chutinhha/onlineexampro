                    National Bank of Oman has appointed Andrew McGregor Duff as 
                    its new Chief Executive Officer. He replaces Dr. B Vasanthan 
                    who held the position for 14 months . Duff’s primary focus 
                    will be to enhance customer service and human resources, as 
                    well as on refining and executing the bank’s business 
                    development and growth strategy and revenue enhancement 
                    policies. In addition, he will also continue to give 
                    emphasis to the recovery process.<br>
                    <br>
                    With over 21 years of diverse banking experience, Duff is a 
                    lawyer turned banker, having worked with Citibank, Standard 
                    Chartered Bank and Commercial Bank of Qatar before. On his 
                    new assignment, Duff said, “NBO had 2-3 tough years. But 
                    that’s history now. It is the right time for me to come on 
                    board as all the problems have surfaced and the bank has 
                    recovered to a great extent. I’m going to lift the morale 
                    among the staff and translate the same feeling among the 
                    customers which is definitely going to enhance shareholders’ 
                    value.” Commercial Bank of Qatar’s acquisition of strategic 
                    stake in NBO and the entry of Duff are beginning to enhance 
                    NBO’s image as some of the employees who left NBO few years 
                    back are beginning to join back the bank.</font></p>
