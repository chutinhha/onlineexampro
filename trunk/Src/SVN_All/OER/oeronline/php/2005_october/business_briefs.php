                        <b>Exports to Thailand cross RO6.4 million</b><br>
                    The trade relations between the Sultanate of Oman and 
                    Thailand have grown at a healthy rate. According to Ahmed 
                    Sulaiman Saleh Al-Maimani, Undersecretary, Ministry of 
                    Commerce &amp; Industry for Administrative &amp; Financial Affairs, 
                    “The exports to Thailand amount to RO6.4 million. The 
                    balance of trade is in favour of Thailand as imports from 
                    Thailand amount to RO31.7 million. The non-oil exports in 
                    2003-2004 to Thai ports increased by 122 per cent while 
                    imports increased by 30 per cent. Exports to Thailand in the 
                    last five years have increased at an average of 19 per cent, 
                    while imports in the same period had an average growth rate 
                    of 21 per cent.”</font></p><hr width="70%" size="1">
