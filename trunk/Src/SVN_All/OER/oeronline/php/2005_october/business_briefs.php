                        <b>OCIPED-KPMG market study in Syria</b><br>
                    OCIPED conducted an export market study for 24 products in 
                    Syria. The research was carried out by KPMG with the support 
                    of Ministry of Commerce &amp; Industry and Public Establishment 
                    for Industrial Estates. Oman’s non-oil exports to Syria 
                    amounted to RO12.6 million compared to RO4.8 million in 
                    2003, registering an increase of 163 per cent. The study 
                    findings are circulated to the concerned Omani export 
                    companies. The study covers the following products: 
                    detergents, biscuits, electric cable, optic fiber cables, 
                    paints, electric transformers, cement, fish and fish meal, 
                    tableware/kitchenware, aluminum profiles, automotive 
                    batteries, mattresses, PVC tubes &amp; pipes, vegetable oil, 
                    fruit juices, marble, BOPP film, metal furniture, wooden, 
                    steel ingots, steel bars, steel tubes, potato chips, ceramic 
                    tiles and sanitary ware. 
