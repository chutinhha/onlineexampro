                        <b>Omani Economic Association Awards</b><br>
                    The Omani Economic Association is going to present its 
                    ‘Annual Awards for Economic Research’ at its annual 
                    conference scheduled on 2 &amp; 3 October. HH Sayyid Asaad will 
                    be the chief guest for the event. The awards are aimed at 
                    reinforcing and encouraging economic research to support the 
                    process of development in Oman; urging Omani and expatriate 
                    researchers to conduct academic researches of interest to 
                    the country and; establishing a research base in order to 
                    assist decision makers. The awards are part of the 
                    association’s endeavour to be an effective partner in the 
                    comprehensive development of the country. In terms of 
                    research area, the focus is on new applied economic research 
                    and proposals for future development in Oman. The research 
                    topics include human resource development, education &amp; 
                    training, small entrepreneurs, finance &amp; banking, commerce &amp; 
                    industry, transportation &amp; shipping, tourism, communication 
                    &amp; information technology, health services, combating 
                    poverty, social cooperation, assessing various government 
                    programmes, investment environment in Oman, systems and 
                    mechanism of various Government sectors functioning, and 
                    methods and means of income diversification. The general 
                    category awards are sponsored by Sh Yaqoub bin Hamed 
                    Alharthy and the student category awards are sponsored by 
                    Polyglot Institute Oman LLC. OER is the exclusive business 
                    magazine partner of the event. <br>
                    <br>
                    The Omani Economic Association has also taken up an exercise 
                    on ‘Business Confidence Index’. The main objective of the 
                    Business Confidence Index (BCI) for Oman is to measure 
                    business leaders’ perceptions of the current economic 
                    environment.</font></p><hr width="70%" size="1">
