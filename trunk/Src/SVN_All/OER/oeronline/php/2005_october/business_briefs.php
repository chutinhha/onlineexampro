                        <b>Oman to attract more FDI</b><br>
                    Oman is expected to further liberalise its economy to boost 
                    foreign direct investment (FDI) in a major way. Maqbool bin 
                    Ali Sultan, Minister of Commerce and Industry informed, &quot;We 
                    are luring foreign investors, and the Ministry of Commerce 
                    and Industry (MOCI) is for reforms in foreign investment 
                    laws as easing of foreign investment regulations will 
                    attract a large number of investors and help them explore 
                    tremendous opportunities.&quot; <br>
                    He added that on an average, Oman’s nominal GDP had grown by 
                    six per cent in the period 1991-2000. The economy grew by 
                    more than 14 per cent in 2004 compared to 6.9 per cent in 
                    the previous year. To continue the march forward, he 
                    cautioned that the Sultanate has to meet the challenges on 
                    two fronts – economic diversification and the development of 
                    human resources. Diversification of the economy, he said, is 
                    government's priority.<br>
                    <br>
                    He further added, “Oman's commitment to the WTO has led to 
                    further liberalisation of the economy, paving the way for 
                    increased opportunities for foreign investments. Investment 
                    opportunities in real estate projects such as the Barr Al 
                    Jissah Resort, the Wave and the Blue City will also play a 
                    key role in liberalising the economy further.&quot;</font></p>
