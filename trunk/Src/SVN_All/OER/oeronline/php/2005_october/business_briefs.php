                        <b>US funds for Oman’s heritage</b><br>
                    HE Richard L Baltimore III, US Ambassador to Oman, has 
                    presented the Historical Association of Oman (HAO) with two 
                    grants from the Ambassador’s Fund for Cultural Preservation. 
                    “We are delighted to be supporting Oman’s unique heritage by 
                    funding two important projects,” Ambassdor Baltimore said. 
                    The first project will enable the HAO to conduct a survey of 
                    Bowshar village to make a database of archaeological and 
                    historic sites, including location, condition and history. 
                    The second grant will fund the HAO as it completes an 
                    architectural, historical and cultural analysis of Bilad 
                    Manah, enabling it to produce a comprehensive book on the 
                    village as a fine example of early traditional settlements 
                    in Oman’s interior. </font></p><hr width="70%" size="1">
