<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>How is Nando’s 
doing globally, especially in the Middle East market?</b><br>
Nando’s in general is doing very well. The brand is getting stronger everyday. 
Within Middle East market, we have some of the most covered networks in terms of 
international brands in the region. We are operating in every single country and 
our objective now is to create the critical mass within those countries so that 
we can begin some of our regional initiatives from a centralized perspective 
like marketing, distribution etc. But what makes us extremely happy is the fact 
that wherever we are operating, we are achieving good success. ME is certainly a 
region where Nando’s has been accepted phenomenally. I think we have the right 
taste profile for the region. <br>
<br>
<b>What is the size of Nando’s global operations?</b><br>
We have more than 1,000 outlets worldwide. And the major hubs are in the UK, 
South Africa and Australia, accounting for two-third of our entire network. 
Nando’s turnover is half a billion dollar at the moment. And again, the major 
contribution comes from our UK, South Africa and Australia operations. <br>
<br>
<b>How has been the growth in the last few years?</b><br>
We are growing at a rate hovering between 15-25 per cent. As we grow bigger and 
bigger, it gets more challenging to maintain or increase the growth rate as the 
base gets wider and wider. <br>
<br>
We don’t follow the strategy of growing our operations only. Our focus is more 
on making Nando’s a strong global household brand. When we look at a market, we 
look at what is the right critical mass for the market so that it doesn’t get 
saturated and where every restaurant can standalone and show profitability to 
its shareholders. Though we have a large number of franchisees in our network, 
we believe that we are in the partnership business. When we look at a place like 
Oman, we jointly look at it in terms of what Nando’s needs to succeed in the 
market. We treat it as if it is our own investment and not of the franchisee. So 
we treat our franchisees as our partners. <br>
<br>
<b>What is the ownership pattern of your global chain?</b><br>
In UK, all our outlets are owned by the Nando’s Group. In South Africa, 40 
percent of the outlets are also group owned. In Australia also, we have got 
significant number of company owned outlets. In other markets, we would like to 
have more company owned outlets but the markets are not conducive for it. They 
are more conducive for franchising route. <br>
<br>
<b>What makes Nando’s offerings distinct from other brands? </b><br>
Talking of competition, broadly speaking, everyone who is in food market is our 
competitor. But I think we have got a unique and distinct product, which is very 
different from others. We offer high quality healthy products along with the 
unique Nando’s experience, which is reflected through the environment inside the 
restaurant, through our service and attitude. It’s a whole lot of basket of 
offer that entices one to come and eat at Nando’s.<br>
<b><br>
Does Nando’s experience remain the same in all the markets or there is variance 
to suit the local culture?</b><br>
There are differences from one market to another. But the core products will 
always be the same in all the Nando’s outlets. The differences are in the 
environment. For example, in the Middle East region, we are positioning 
ourselves as a casual dining outlet. In South Africa, we started as a take-out. 
In South Africa, we are at the top end of fast food segment. But here, we are in 
the casual dining segment. So you will find that here, we have 100-per cent 
eat-in whereas in South Africa, we have 70 per cent take-outs. So those are the 
differences. <br>
<br>
We strive to go by the requirements of the local culture. We want our customers 
to feel comfortable as much as they possibly can within a restaurant. So we try 
to bring in elements that our customers can identify with and give them the 
comfort level.<br>
<br>
<b>Have you created some special products to cater to the taste of markets like 
Oman?</b><br>
Well in terms of the core products, the adjustments are more from the heat level 
rather than from a flavour profile because we believe that flavour profile is 
going to be the same wherever we are. We bring the local flavour in side items. 
We believe that if one wants to eat local food, one would go to a local 
restaurant. Nando’s offers a different experience. It offers what you don’t 
have.<br>
<br>
<b>What has been the role of advertising in building Nandos’ brand?</b><br>
The role of advertising has been very simple. One, it is to have fun and two, it 
is to convert one-dollar investment into five dollars revenue! With so much 
clutter in the market and when you are competing against giants that have got 
huge ad budgets, one has to be different in one’s communication. One has to find 
ways of standing out. And that’s been the role of Nando’s marketing in general. 
We constantly try to think totally out of the box which people could relate to 
and which makes them laugh. <br>
<br>
<b>With such an extensive network spread globally, how do you ensure uniform 
quality standards?</b><br>
It is definitely a huge challenge but we have got a large team in place that 
supports every single market. The team members travel extensively. We have got 
mechanisms through certain processes that ensure consistency. The Nando’s team, 
no matter where it is located, is trained in every aspect related to ensure our 
high quality standard products and customer services <br>
<br>
<b>What are your plans for Oman market?</b><br>
At the moment, we have one outlet here. Our plan is to grow the brand and make 
it stronger. We want to grow in certain areas to make ourselves more accessible 
to our customers. We have identified a few sites here already and we look 
forward to take the growth process forward.</font></p>
