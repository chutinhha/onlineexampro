<b>How is advertising business in Oman different from global trends?</b><br>
There was no advertising in Oman two decades ago. I can say that only during the 
last decade we have seen the advertising happening and growing with controlled 
speed. We cannot compare advertising business in Oman to global trends. Oman has 
just started accepting and understanding the power of advertising. Oman is a 
country that has by and large operated on short-term vision policies in the 
marketing and communication arena with the exception of a &quot;few brands&quot;. It is 
only in the last 5 years or so that more players are making a long-term 
conscious effort towards brand building.<br>
<br>
<b>Why advertising from Oman fails to get any recognition at international 
platforms like Cannes Awards? </b><br>
Here, as I have mentioned, very few get into brand building exercise. There is a 
fair reason for it- most of the brands are imported. Most of them produce here 
only tactical/topical campaign. The safe route is the best route and most of the 
doyens of advertising teams at clients still are very firm custodians of this 
belief and thereby do not allow any breakthrough strategy of creativity. 
Needless to say most of the best work produced in most of the major agencies in 
Oman lies in agencies job bags.<br>
<br>
