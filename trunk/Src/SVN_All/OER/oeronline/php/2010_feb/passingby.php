				<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				</font><font size="2" face="Verdana"><b>Destination of choice<br>
				</b></font><font face="Verdana" size="1"><br>
				<br>
				<img border="0" src="images/passingby01.gif" align="right" hspace="8" vspace="4" width="200" height="252">Invest in America promotes and supports foreign direct 
				investment (FDI) in the US. It is the primary US government 
				mechanism to coordinate inward investment promotion. Its efforts 
				are focused on outreach to foreign governments and investors. 
				Aaron Brickman, director, Invest in America, visited Oman 
				recently as a part of a series of planned visits from senior US 
				officials to promote FDI from the Sultanate. Mayank Singh speaks 
				to him about the impact of the financial crisis on FDI flows, 
				the changing perception of the US as the destination of choice 
				for capital and visa concerns. Excerpts<br>
				<br>
				<b>What is the purpose of your visit to the Sultanate of Oman?<br>
				</b>I am on a visit to various countries in the region like 
				Saudi Arabia, Qatar, Abu Dhabi and Oman to raise the profile of 
				the Invest in America programme. The US has been an investment 
				destination of choice for decades. FDI creates jobs and promotes 
				economic growth. Free Trade Agreements (FTA’s) with various 
				countries have created five and a half million jobs in the US, 
				so it is good for us, but it is also a win-win situation for the 
				country of origin because it leads to technology transfer, know 
				how gains in terms of management techniques etc. In 2008, the US 
				received FDI worth $320bn. The purpose of this visit is to 
				strengthen this message. <br>
				<br>
				<b>What kind of FDI flows has the US been attracting from Oman 
				and the Gulf region?<br>
				</b>We have not seen as much FDI interest from Oman as we would 
				like, but there is a wider interest in investing in the US from 
				the Gulf region. A lot of this investment takes place through 
				securities. But if we look at the larger picture, Arab 
				investment is a very small part of the overall FDI inflows to 
				the US. Talking about splits, Western Europe accounts for 71 per 
				cent of FDI inflows into the US, this is followed by Japan, 
				Canada and Australia. Together these countries make up 85 per 
				cent of FDI into the US. <br>
				<br>
				<b>Has the financial crisis in the wake of the sub prime crisis 
				affected FDI flows to the US?<br>
				</b>During the recessionary period, in comparison to other 
				developed economies, the US has fared much better. The UK, 
				Netherlands and Germany have seen a decline in FDI inflows in 
				contrast the US has continued to attract as much investment in 
				2009 as in 2008. So we are holding even. In times of crisis, the 
				US remains a destination of choice. Historically, investors have 
				flocked to gold and the US in times of a crisis.<br>
				<br>
				<b>The US was seen as a low-risk-low return market, so even if 
				the returns were less compared to similar investments made in 
				developing countries it was acceptable to investors. But the 
				global financial crisis and its impact has shown that the US is 
				as vulnerable to a financial crisis as any other country. Will 
				this perception impact America’s ability to attract FDI inflows 
				in the future?<br>
				</b>Nobody ever guaranteed returns on the stockmarket. As a 
				country the US has been resilient to economic downturns for the 
				last 100 years and has given the best risk adjusted returns over 
				the years. We are a low risk country and so there are lower 
				rewards compared to other countries but it remains indisputably 
				the most predictable operating environment in the world. This 
				helps in attracting a constant flow of FDI. A Daimler C class 
				plant has been set up in Alabama recently and there is a steady 
				trickle of mid-sized companies in the last few years to the US 
				for a variety of reasons.<br>
				<br>
				<b>What makes US an attractive destination for FDI?<br>
				</b>The US offers a fully developed market of 320mn people with 
				an expanding population. It represents over 40 per cent of the 
				world’s consumer goods market and 35 per cent of the luxury 
				products market. It has a per capita income of $30,000. All 
				these factors attract a steady stream of business. Almost half 
				of the patents from other countries are registered in the US. 
				This reflects the intellectual know-how of the country. The 
				country offers good infrastructure in the shape of roads, 
				railways, information technology (IT), airlines etc. These 
				factors have helped us to hold onto FDI inflows. And we believe 
				that even in these challenging times there are opportunities. 
				The US has had historical ties with various countries and has 
				traditionally been attracting investments from these countries, 
				but it is also increasingly attracting investments from India 
				and China. <br>
				<br>
				<b>Though Oman and the US have signed a Free Trade Agreement (FTA), 
				there are apprehensions that the difference in scales between 
				companies in the US and the Sultanate will make local companies 
				uncompetitive in the US. Are such concerns justified? <br>
				</b>FTA’s have always encouraged more investments between 
				signatories. Year-on-year we have seen more such agreements 
				being ratified with various countries like Chile, Costa Rica, 
				Singapore, Oman and so on. If you look at sectors like IT and 
				biotechnology almost all companies start off small. The US has 
				so many success stories like Intel Corporation, Yahoo, Google, 
				Hewlett Packard – all of which have started off as small 
				companies. A good idea, the right plan and strategic partners is 
				the recipe for success in the US. It is important for Omani 
				businesses to study how others have engaged themselves in the US 
				market and to do their due diligence properly. This requires 
				investment in terms of time and resources. Invest in America can 
				be of help here as it continually develops programmes and 
				services to educate international investors on topic related to 
				Investing in the US. We also organise a number of world class 
				trade shows in various countries.<br>
				<br>
				<b>There is a perception that people from the Middle East find 
				it difficult to get visas to the US. Is this a deterrent to 
				attract FDI from the region? </b><br>
				The world has changed since 9/11. In its wake a lot of entry 
				procedures and visa restrictions were put in place for national 
				security. Things have changed since then, the State Department 
				in the US has been working on managing the process better. <br>
				<br>
				Though things are far from ideal in terms of equal treatment, a 
				lot of effort is on to streamline procedures. An occasional 
				negative story makes bigger news than the fact that numerous 
				people travel everyday to the US without any problems. Oman for 
				instance has a very low visa refusal rate. <br>
				<br>
				<b>The Dubai Port World episode got a lot of negative publicity 
				in the regional press giving credence to stories that 
				investments from the Gulf region was not welcome in the US. How 
				would you react to this charge?<br>
				</b>Since DP World, a number of companies from the region have 
				come and invested in the US. A notable example being Dubai 
				Aerospace. So DP World should be treated as an exception and not 
				as the norm. <br>
