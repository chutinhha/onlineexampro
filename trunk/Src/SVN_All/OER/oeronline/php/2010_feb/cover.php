				<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr">
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <b>Construction benefits<br>
                    </b><br>
                    <b> 
                <img src="images/coverstory04.jpg" hspace="9" vspace="5" border="0" align="right" width="120" height="149"></b>The 
                biggest beneficiary of this year’s budget is going to be the 
                construction sector, whether it is for roads, buildings or 
                engineering fields. This is indicated by the budget allocations 
                of 937mn rials for new projects followed by education (847mn 
                rials); and health (294mn rials). The increased allocation is 
                sure to translate into new hospitals, educational facilities and 
                roads. <br>
                <br>
                It is worth mentioning that the new housing units to be 
                constructed in the various areas of the Sultanate during the 
                Seventh Five-Year Plan amounted to about 4,989 units. There have 
                been additional approbations for construction, development and 
                expanding of Al Duqm port by 788mn rials; additional 
                approbations for airports sector for Muscat airport (513mn rials) 
                and 121mn rials for airport development of Sohar, Al Duqm, Ras 
                Al Hadd and Adam.<br>
                <br>
                Philip Paul, head of country, Cluttons Chartered Surveyors, 
                Property Consultants, says that if you look at the overall 
                economy, there is a significant movement from old areas to new 
                primarily because of growth in economic activity and the need 
                for companies to expand their operations. It is more like an 
                evolution of the city. One such example is BankMuscat which is 
                building a new head office at Airport Heights. <br>
                <br>
                Another example, is the demand for affordable housing, which is 
                going up as more nationals attain purchasing power. “Thus, there 
                is not only a natural progression of the city by expanding 
                outward, but the economy itself is creating more demand for 
                office space and residential accommodation which means that more 
                infrastructure facilities like houses, roads, water and power 
                utilities have to be built. Apart from these there is a move to 
                re-develop existing areas like Ruwi, CBD and the Muttrah 
                Corniche, all of which will create a knock-on effect,” adds 
                Paul. <br>
                <br>
                    <b> 
                <img src="images/coverstory05.jpg" hspace="9" vspace="5" border="0" align="right" width="120" height="140"></b>Says 
                Francis Selvaraj, associate partner, Cluttons, “Despite the 
                global doom and gloom situation, Oman has fared better though 
                the demand for sales and leasing of properties declined. Earlier 
                it (the demand) was because of speculation, but when the prices 
                came down, it was a real correction as actual buyers entered the 
                market.”<br>
                <br>
                “There will be a qualitative shift in rental and office 
                accommodation as real estate projects get approved and work 
                begins. A substantial amount of office space will be added if 
                organisations want to upgrade the quality of their offices and 
                seek movement,” says Selvaraj.<br>
                <br>
                For example, Cluttons is working with Oman Air Management 
                Services (OAMS) to develop a real estate master plan for the 
                peripheral airport area. The company will be advising OAMS about 
                office space, logistics, retail related areas like an airport 
                city etc. The development will run in parallel with the actual 
                airport development.<br>
                <br>
                The housing sector is also expected to contribute to 
                inflationary pressure, which will be created by the recovery of 
                the global economy in 2010 and the rise in commodity prices. It 
                is important that economic growth is achieved with a reasonable 
                inflation. In this respect, it was good to note that during the 
                first nine months of 2009, inflation was estimated at 3.6 per 
                cent compared to 12.4 per cent in 2008. It is reasonable to 
                presume that the inflation would not be significantly higher 
                during 2010, adds Hariharan.</font></p>
