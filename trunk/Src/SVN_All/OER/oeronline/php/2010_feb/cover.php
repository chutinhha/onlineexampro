				<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
                    It is noteworthy that oil and gas revenues finance about 68 
                    per cent of the public expenditure, whereas the non-oil revenues 
                    finance about 21 per cent thereof. There has been a considerable 
                    improvement in the contribution of non-oil revenues over the 
                    Seventh Five-Year Plan period. The finance rate of oil and 
                    gas revenues decreased from 83 per cent to 68 per cent, from 
                    2006 to 2010 against an increase in the finance rate of the 
                    non-oil revenues from 17 per cent to 21 per cent.<br>
                <br>
                Based on the $50 per barrel budgeted price, oil revenues are 
                estimated to amount to about 4.050bn rials i.e its contribution 
                shall be at about 63 per cent of the total revenues, whereas the 
                contribution of the gas revenues will be 800mn rials or 13 per 
                cent. The current and capital revenues are estimated to be about 
                1.530bn rials ie an increase of eight per cent over the 2009 
                budget or 24 per cent of the total revenues.</font></p>
