                    The local consumption story remained strong during the 
                    last fiscal year on the back of robust government spending 
                    towards economic diversification, which in turn lead to increased 
                    private sector investments in the country, Kailasam says. 
                    <br>
                    <br>
                    Commenting further, he says the government has the taken right 
                    steps in offering a development-oriented budget in 2010 by 
                    maintaining its fiscal spending and focus on job creation, 
                    which is beneficial for economic growth in the long run. Taking 
                    into consideration the consensus that oil price will stay 
                    above $70 per barrel in 2010, along with the continuing support 
                    from the government towards increased fiscal spending and 
                    economic policies, it is likely that the pace of growth and 
                    development wo;; acceleterate. In fact, the government may 
                    end the year with a surplus provided the global prices remain 
                    the same. Moreover, development programmes would in turn be 
                    the key hauler for the private sector investments in the country. 
