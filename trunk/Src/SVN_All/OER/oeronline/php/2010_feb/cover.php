				  <p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                    <font size="2" face="Verdana"><b><br>
                    Injecting Confidence<br>
                    </b></font> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
                    <b> 
                    <img src="images/coverstory01.jpg" hspace="9" vspace="5" border="0" align="right" width="120" height="160" style="border: 1px solid #C0C0C0">All eyes were on 2010 General State Budget as it came in 
                    the aftermath of the global economic crisis. HE Ahmed bin 
                    Abdulnabi Macki surpassed expectations by presenting the 
                    biggest budget in decades and reinvesting surpluses of the 
                    past few years into the economy to meet the objectives of 
                    the seventh five-year plan. Visvas Paul D Karra tracks a few 
                    key indicators of the budget in this report<br>
                    </b><br>
                    <br>
                    Like the rest of the region, Oman’s economy is largely 
                    dependent upon a single commodity -- oil and therefore any 
                    reading of the State Budget 2010 has to take into account 
                    the fortunes of oil during the turbulent period in the past 
                    year. Commodity prices rallied back in 2009 on the back of 
                    the global economic recovery and improved demand from 
                    emerging economies like China, India etc. In 2009, oil 
                    prices bounced back to $70 per barrel by the middle of the 
                    year after reaching low of $36 a barrel in February. <br>
                    <br>
                    The government of Oman budgeted oil price at $45 per barrel 
                    in 2009 whereas the actual realisation was $56.7 per barrel 
                    after the prices dropped from $101.1 per barrel in 2008, 
                    reflecting a decrease of 44 per cent. This decrease was, 
                    however, compensated by the hike in daily oil production, 
                    which increased by seven per cent in 2009 to around 810,000 
                    barrels per day up from 756,800 in 2008.<br>
                    <br>
                    Despite additional projects, which were sanctioned during 
                    the year, and the financial approbations, which were 
                    allocated to cover the different elements of the public 
                    expenditure in 2009, a large part of the estimated deficit 
                    of 810mn rials was covered by oil prices, which peaked at 
                    $70 per barrel by the last quarter of the year.<br>
&nbsp;</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">&nbsp; </font> 
                  <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                  <hr width="98%" size="1">
                  </font>
                  <p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><b>
                  <font size="1" face="Verdana">Conservative</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    pricing </font> </b><font size="1" face="verdana"><br>
                    <br>
                    The 2010 budget is based on an oil price of $50 per barrel 
                    (versus $45 in 2009). Says Ashok Hariharan, Partner, Tax - 
                    Oman and UAE, KPMG, “as is the practice for several years, 
                    the government is quite conservative in its budgeting and 
                    has rightly considered for the budget an average oil price 
                    of $50 per barrel. This contrasts with the actual average 
                    oil price of $57 per barrel achieved in 2009 and an average 
                    price of $70-80 per barrel expected by experts during 2010.”<br>
                    <br>
                    Despite the presence of lower oil prices during the first 
                    half of the year, and the slowdown of global economy, the 
                    national economy is expected to report a positive growth of 
                    3.7 per cent at real GDP in 2009. However, the contraction 
                    rate of 2009 nominal GDP (at current prices) is expected to 
                    be above 20 per cent. As per the latest data of Ministry of 
                    National Economy, the nominal GDP has declined by 27.2 per 
                    cent to 12.760bn rials at the end of September 2009.</font></p>
                  <hr width="98%" size="1"> 
                  <p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr"> 
                    <b><font size="1" face="Verdana">Salient</font><font size="2"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> </font></font>
                    <font size="1" face="Verdana">features</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> <br>
                    </font>
                    </b> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    <b> <img src="images/coverstory02.jpg" hspace="9" vspace="5" border="0" align="right" width="120" height="169"></b>The 
                    biggest highlight of the 2010 Budget is that it is considered 
                    the biggest State Budget so far with an ambitious layout. 
                    The total revenues are estimated at 6.38bn rials – an increase 
                    of 14 per cent over the 2009 budget. Total estimated expenditure 
                    is also up by 12 per cent to 7.18bn rials. On a segmental 
                    basis, oil and gas revenues contribute around 76 per cent 
                    of the total revenues, whereas the current and capital revenues 
                    amount to 24 per cent of total revenues.<br>
                    <br>
                    The economy is expected to grow by 6.1 per cent at constant 
                    prices and 18.4 per cent at current prices in 2010 and inflation 
                    rate is set to stabilise at 3.5 per cent. The budget is expected 
                    to result in a deficit of 800mn rials in 2010 as against the 
                    810mn rials estimated deficit in the previous budget. The 
                    deficit is 13 per cent of the revenues and three per cent 
                    of the gross domestic product (GDP). As a percentage of the 
                    GDP this is considered as being economically safe and acceptable. 
                    <br>
                    <br>
                    This confidence stems from the fact that the national economy 
                    showed an ability to counter and confront the repercussions 
                    of the large decline in oil prices last year. Says HE Ahmed 
                    bin Abdul Nabi Macki, Minister of National Economy, who is 
                    Supervisor of Ministry of Finance and Deputy Chairman for 
                    Financial Affairs and Energy Resources Council, “The ability 
                    of the national economy to counter this external shock stems 
                    from a number of factors mainly the increase in oil production 
                    rates, the economic policies aimed at further economic diversification, 
                    encouragement of investment and the expanding fiscal policies 
                    adopted by the government to support domestic demand.” Similarly, 
                    monetary policies and other measures undertaken for liquidity 
                    availability and support of the banking and financial sector 
                    stability in general also contributed to this.<br>
                    <br>
                    According to Hariharan, an indication of the state of the 
                    economy is provided by the government’s estimated revenue 
                    from taxes and fees. The increased revenue budgeted from taxes 
                    and fees would suggest that economic activity in 2010 would 
                    be much better than in 2009.<br>
                    <br>
                    Revenue from income tax alone is projected to go up by 33 
                    per cent compared to the 2009 budget to 300mn rials in 2010.<br>
                    <br>
                    It is understood that collections from income tax during 2009 
                    significantly exceeded the 2008 budget of 225mn rials. The 
                    reduction in foreign company tax rates from 30 per cent to 
                    12 per cent is not likely to have an effect on the 2010 revenues, 
                    as this would largely impact collections only in 2011. It 
                    is worth noting that the government’s budgeted collection 
                    from income tax during 2008 was only 166mn rials. In two years 
                    time i.e. in 2010, the government is budgeting a revenue of 
                    300mn rials despite there being no increase in tax; this is 
                    80 per cent higher than the budgeted collections for 2008.<br>
                    <br>
                    The government is also expecting a marginal increase of seven 
                    per cent in revenue from customs duties budgeted at 160mn 
                    rials and three per cent (110mn rials) from non-Omani labour 
                    licences. This contrasts with a 50 per cent increase in budgeted 
                    revenue from customs duties during 2009 and a 24 per cent 
                    increase in budgeted revenues in 2009 from non-Omani labour 
                    licences. The increase in customs duties and non-Omani labour 
                    licence revenues in 2010 are significantly smaller than those 
                    resulting from the 2009 budget possibly reflecting the impact 
                    of Free Trade Agreements and continued focus on Omanisation.</font></p>
                  <hr width="98%" size="1">
                  <p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr"> 
                    <b><font size="1" face="Verdana">Five-year 
                    plan impact</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    </font>
                    </b> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    <b> <img src="images/coverstory03.jpg" hspace="9" vspace="5" border="0" align="right" width="200" height="179"></b>This 
                    year’s budget must be viewed in the light of the Seventh Five-Year 
                    Development Plan (2006-2010). The five-year development plans, 
                    which are part of the Vision 2020 development document are 
                    considered to be the prime mover and executive tool of the 
                    budget. According to HE Macki, it should be noted that 2010 
                    is the last year of the Plan and as such, the government has 
                    continued to allocate finances for its development programmes 
                    to fulfill its commitments for completion of infrastructure 
                    projects. With this in mind, all the surpluses of the previous 
                    budgets have been utilised till November 2009 and there has 
                    been a large increase in size of approbations for development 
                    programmes.”<br>
                    <br>
                    HE Macki disclosed that most of the projects were in response 
                    to the Royal Directives during the Royal Tours, which explore 
                    the pressing needs of citizens in all the wilayats and regions 
                    and work for their fulfillment along with the implementation 
                    of a number of development programmes. Additional funds were 
                    also allocated for rehabilitation of infrastructure which 
                    was affected by the unusual weather conditions of June 2007 
                    totaling 494mn rials.<br>
                    <br>
                    Thus, there was a whopping 221 per cent increase in approbations 
                    year-on-year with a total of 6.665bn rials for the development 
                    programme taking the allocations to 9.681bn rials as compared 
                    to the approved 3.016bn rials at the start of this five-year 
                    plan. “The continuance of government public spending towards 
                    achieving its objectives of the seventh five-year plan is 
                    expected to create more new jobs which in turn would increase 
                    local consumption, forming the cornerstone for economic growth. 
                    The efforts taken towards the completion of projects along 
                    with the additional approbations done in the development programme 
                    would in turn be the key hauler for the private sector investments 
                    in the country,” says Sankar Kailasam, senior vice president 
                    (Asset Management), Gulf Baader Capital Markets.<br>
                    <br>
                    The Omani economy remained resilient in 2009 on the back of 
                    increased infrastructure spending along with the presence 
                    of favourable monetary policies and the support to the local 
                    banking system by means of availability of liquidity. Kailasam 
                    believes that the government’s role in maintaining the stability 
                    in economy would in turn provide continued growth, offering 
                    strength to the economy in the current situation as well.<br>
                    <br>
                    The additional approbations during the past years were for 
                    a number of projects in various sectors like roads (1.777bn 
                    rials); ports (1.025bn rials); housing (406mn rials); airports 
                    (882mn rials); health (247mn rials); town planning and municipalities 
                    (252mn rials); and education (119mn rials). In addition to 
                    this, total approbations for the new projects to be implemented 
                    in 2010 is pegged at 937mn rials.</font></p>
                  <hr width="98%" size="1">
				<p style="margin-left: 10px; margin-right: 10px" align="justify" dir="ltr"> 
                    <b><font size="1" face="Verdana">Kharusi</font><font face="Verdana, Arial, Helvetica, sans-serif" size="2"> </font>
                    <font size="1" face="Verdana">Employment</font><font face="Verdana, Arial, Helvetica, sans-serif" size="2"> </font>
                    <font size="1" face="Verdana">generation</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b><br>
                    </b><br>
                    The government’s thrust as always has been on infrastructure 
                    development and employment generation. The total expenditure 
                    for 2010 has been estimated at about 7.180bn rials as against 
                    6.424bn rials in 2009, an increase of 12 per cent year-on-year. 
                    Of this, the total current expenditure for the year is forecast 
                    to be 4.432bn rials, forming around 62 per cent of the total 
                    expenses.<br>
                    <br>
                    The government has budgeted 10.9 per cent increase in investment 
                    expenditure for 2010 to 2.128bn over the previous year’s budgeted 
                    amount of 1.919bn rials. The budget has allotted 30mn to cover 
                    the operational expenses of the new projects intended to be 
                    operated for ministries during the year 2010. This would result 
                    in the creation of 4,000 new job opportunities in the labour 
                    market. It is believed that the government’s thrust on employment 
                    generation would in turn help local consumption growth thereby 
                    aiding economic growth. Adding up to this, the project investments 
                    would help in increasing the private sector investments.<br>
                    <br>
                    “In general we are seeing a very optimistic budget which is 
                    definitely going to give a good impression to everybody, both 
                    to the market and investors. This will give confidence to 
                    the people that the economy is growing and the development 
                    spending is going on, as we know that the economy depends 
                    upon investments and expenditures. It gives us a signal that 
                    we have a healthy economy and it is going to be a good year 
                    despite the consequences of the past year,” says Abdullah 
                    Salem Al Salmi, executive vice president, Capital Market Authority.<br>
                    <br>
                    Referring to the MSM, which bounced back in the second half 
                    of 2009, Al Salmi says, “Once the results of the companies 
                    are out, a lot of doubts will be cleared and clarified and 
                    investors will know where they are standing. The annual results 
                    are emerging and this will clear up a lot of issues and we 
                    are quite confident about the performance of the banking sector. 
                    According to CBO figures, the money market was distinguished 
                    by the growth of money supply where the domestic liquidity 
                    increased by 5.6 per cent by the end of October 2009 as compared 
                    to the same period in 2008.”</font></font></p>
