				<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
				</font><font size="2" face="Verdana"><b>Brand Power</b></font><font face="Verdana" size="1"><br>
				<br>
				<b>Opening a franchise always seems to be a rich man’s business. 
				Is this a myth or a reality? Malcolm Xavier Crasta explores the 
				issue</b><br>
				<br>
				</font><font size="2" face="Verdana"><b>
				<img border="0" src="images/franching.gif" width="200" height="244" align="right" hspace="9" vspace="5"></b></font><font face="Verdana" size="1">Burgeoning 
				under the impact of commercial growth in past few years, 
				franchises are spread all over Oman, ranging from big retail 
				brands to cafés and fast-food joints. A competitive environment 
				thrives, especially in the food and beverage industry. Inspired 
				by the boom in industrial sectors and the success of numerous 
				outlets, many people are now considering operating a franchise. 
				However, to do so, there are three key things that one would 
				need to know: an overview of the business in Oman as seen in 
				global perspective; common mistakes that entrepreneurs make, and 
				finally, what one needs to be prepared for.<br>
				<br>
				<b>Franchising in Oman<br>
				</b>Generally speaking, franchising is a smart business practice 
				of putting into use another person’s business model. The 
				franchisor grants the independent operator the right to 
				represent its products, techniques and trademarks for a slice of 
				gross monthly sales and a royalty fee. Various support services 
				like training and marketing are usually provided by the 
				franchisor to the operator. Agreements by and large last from 
				five to thirty years. As in other parts of the globe, in Oman a 
				franchisee pays a franchisor royalty fees while signing the 
				agreement, thereby gaining access to the brand, the business 
				model and support system. Turnover can be boosted without 
				increasing assets or infrastructure.<br>
				<br>
				</font><font size="2" face="Verdana"><b>
				<img border="0" src="images/franching01.gif" width="200" height="262" align="right" hspace="9" vspace="5"></b></font><font face="Verdana" size="1">The differences are only marginal when Oman is compared to the 
				rest of the world. Whatever variation is there is simply because 
				Oman is a smaller market vis-a-vis the West. Here one is 
				allocated the entire Sultanate as an exclusive territory, while 
				in the West the territory allocation is based on demographics. 
				“For example, the franchise for Second Cup in Oman is owned 
				entirely by us whereas Second Cup Canada has franchisees for 
				every outlet,” says Jannat Shafaei, marketing director, Bin 
				Mirza International, which is also the franchisee for Nandos, 
				Cinnzeo and Breadtalk.<br>
				<br>
				Very often the entire Middle East is taken as a unit for 
				franchise and then each country is sub-franchised. “For Second 
				Cup, we are the sub-franchisee. The master franchisor is in 
				Dubai and he has the whole Middle East, while he sub-franchises 
				each country. But for Nandos, we have the master franchise for 
				Oman and it is the same for Breadtalk. While we own the master 
				franchise, we have the liberty to sub-franchise the brand in 
				Oman and that is generally how it is done,” adds Hani Ali Mirza, 
				managing director, Bin Mirza International.<br>
				<br>
				As far as fees are concerned, it is the middleman that rakes in 
				money and as such a master franchisee, so to say, enjoys his 
				situational advantage. Those placed in the lower rung, bear the 
				brunt of the entire process and have to shell out more. Jannat 
				adds, “For Second Cup we pay Dubai and they pay Canada whereas 
				with Nandos we pay South Africa directly as there is no middle 
				man, so to speak.” So, if one were to take the whole region, the 
				franchisee is more likely to have lower rates and on top of that 
				he/she would make money from the sub franchisees as well.<br>
				<br>
				</font><font face="Verdana" size="1"><b>Starting a franchise<br>
				</b>Though it is expensive to own a franchise, but one can have 
				it at lower rates also. The cost to setup a sub franchise is 
				significantly lower than a master franchise, despite the 
				presence of a middleman. Many franchises do not have a master 
				franchise in the region at all. Instead, with limited number of 
				stores per area, they work on a store-to-store basis, where each 
				store is run by a different operator. It is a good model to 
				start with, though it may be risky for people with lower 
				incomes, as a lot of care has to be taken in handling them 
				simply due to the fact that the owners of the master franchise, 
				wherever he is located, will take the fee regardless of the 
				profits made by the business. <br>
				<br>
				</font><font size="2" face="Verdana"><b>
				<img border="0" src="images/franching02.gif" width="200" height="204" align="right" hspace="9" vspace="5"></b></font><font face="Verdana" size="1">Kim Jepsen, general manager, Markaz Al Bahja and Oasis 
				Lifestyles, the franchisee of IDdesign, bodum and ZONE, says, 
				“Most franchises I know, in addition to the royalty and 
				franchise fee, earn money from their products, shop fittings, 
				training sessions and all that you will purchase from them. The 
				regular fees are only a part of the cost albeit a major portion 
				of it.” As such, a franchisee would need to have enough reserve 
				money to sustain himself on a long term basis. “There are a 
				number of franchises that are very cheap and then there are 
				franchises like IDdesign. While I cannot disclose how much 
				IDdesign costs, I can tell you that it is not cheap and when you 
				are looking at furniture retail you are looking at four-to-five 
				years before you have returns on your investments,” adds Jepsen.<br>
				<br>
				<b>Time and freedom<br>
				</b>Opening a franchise anywhere is a time consuming process as 
				it involves a gamut of procedures and queries. “I think if 
				anyone is taking over a franchise and the franchisor isn’t 
				following a structured due diligence process and at the same 
				time is not forthcoming with answers to the queries which the 
				operator or franchisee may have, I would be worried about taking 
				over the franchise,” says Ajit Singh, general manager-Retail, 
				Asha Enterprises, the franchisee for Barista. Keeping this in 
				mind, it is advisable to enter into a relationship with a brand 
				that is protective over what they do because, in general, the 
				more protective the franchise, the more favourable it is for the 
				operator.<br>
				<br>
				This doesn’t, however, mean that one cannot enjoy any freedom at 
				all, though it may vary from franchise to franchise. In general, 
				a franchisee is allowed to make changes based on the market and 
				within the bounds of the agreement. Usually, but not always, 
				autonomy can be exercised so long as the changes do not affect 
				the concept of franchise that is in place. <br>
				<br>
				Franchising became popular for decentralised organisational 
				structure at the end of the 20th century. Elements from the 
				concept of franchising have been used, tried and tested and 
				changing them in any way and adapting them to the local market 
				defies the point of obtaining the franchise in the first place.<br>
				<br>
				The operator also needs to be aware about his own market and be 
				in constant dialogue with the concerned people to keep them 
				aware of his efforts in maintaining guidelines. “As long as you 
				have got that openness from the beginning, especially in this 
				part of the world, then they tend to be quite flexible and will 
				be willing to work with you,” adds Singh.<br>
				<br>
				<b>Common mistakes <br>
				</b>One of the major mistakes made by people is the lack of 
				research on the viability of the brand in the country and the 
				location. A business that may have boomed elsewhere may not find 
				a market in another region. One has to consider differences in 
				climatic conditions, socio-cultural characteristics, and the 
				spread of sparse population in clusters in the Sultanate and a 
				host of other factors before getting a franchise into Oman. <br>
                <br>
				Sharing his views on the subject, Nick Braxton-Abery, country 
				manager, Emirates Leisure Retail, which runs Noodle House says, 
				“There are instances where a franchise is successful in one 
				market but fails in others. There could be a number of reasons 
				for this; the support from the franchisor could be inadequate or 
				the local franchisee may not be a suitable partner.” Brand 
				recognition is also an important criterion and bringing in a 
				brand that is not recognised, is not necessarily a good idea, 
				unless it has unique features.<br>
				<br>
				</font><font size="2" face="Verdana"><b>
				<img border="0" src="images/franching03.gif" align="right" hspace="9" vspace="5" width="200" height="206"></b></font><font face="Verdana" size="1">Also, while people think that they are dealing with a fixed set 
				of norms, in reality there is always room for negotiations, 
				especially when it comes to rates, fees, royalties and more. It 
				is imperative that a franchisee takes a close look at the 
				franchisor and his setup and more importantly, talks to other 
				franchisees and get their feedback on the company before 
				signing. It is important to explore whether they are satisfied 
				with the &#1607; from the franchisor as that is one of the key 
				necessities of a successful franchise. <br>
				<br>
				Sometimes in the excitement of obtaining the franchise, one’s 
				judgment can get clouded whereby the contract is not read in its 
				entirety (neither handed over to a lawyer to be read and obtain 
				his feedback). This could land the franchisee into trouble or he 
				may end up with a raw deal.<br>
				<br>
				It is also important to be passionate about the business that 
				one plans to open though not just assume that the business would 
				do well in Oman. This is also a key point that franchisors look 
				for in prospective operators as it shows the amount of 
				involvement one would willingly have in the business, primarily 
				because the franchisee will be representing their brand in Oman. 
				If one were an engineer, it would be advisable to look for a 
				franchise related to his profession, as it would be easier for 
				the franchisee to grasp nuances of the business and at the very 
				least when one is negotiating with the franchisor, he will be 
				speaking the same language.<br>
				<br>
				<b>Scope for more<br>
				</b>While it may seem that the market is already saturated with 
				established businesses, there is still scope for opening a 
				successful franchise as long as one looks for a niche or a 
				specific product that is not yet available in the market. 
				Besides, a place like Oman is still growing and there is a lot 
				that has not happened yet. Consumers in Oman are very different 
				from what one would find elsewhere. They are brand conscious and 
				as mentioned earlier, they are a sparse population, spread all 
				over the country in clusters. Hence, chances of spreading 
				business by word of mouth is not always easy. It is a tough 
				business but franchising, in the end, is an easier way of 
				operating a business for someone who wants to be an 
				entrepreneur. It is a tried and working business model and no 
				one has to reinvent the wheel. <br>
				<br>
				However, one should be aware that financial institutions in Oman 
				have adopted a cautious approach, especially in the light of the 
				global economic downturn. So it is imperative that one has 
				access to the necessary finances prior to entering the business.<br>
				<br>
				<b>The bottomline<br>
				</b>The most important factor in a lot of failed businesses is 
				the inability of the operator to sustain the business through 
				its initial few months prior to the business being opened. 
				Government approvals and procedures can take a lot of time, 
				especially when it comes to a franchise, so one would have to 
				make sure that he has enough money to sustain the business for 
				the first few months prior to the business being opened and even 
				for the first few months after it has opened. Where many people 
				also go wrong is that they don’t think about cash flows. So in 
				addition to already having the startup capital, it is just as 
				important that one also takes into account the money needed to 
				sustain the business until the franchisee begins to make 
				profits. <br>
				<br>
				“<i>Successful franchises are a day-in and day-out commitment 
				where both parties, the franchisee and franchisor, have to 
				ensure that the brand is always being nurtured and constant 
				effort and innovation is going into new products,</i>” concludes 
				Braxton-Abery. The more positive interactions there are between 
				the two parties, the better are the chances to run a successful 
				money-spinner franchise. 
