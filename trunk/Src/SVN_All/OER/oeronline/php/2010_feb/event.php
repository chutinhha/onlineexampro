                    <font face="Verdana" size="1"><br>
                    </font><font size="2" face="Verdana"><b>OER CEO Golf 2010: 
                    Tee off on March 11 </b></font><font face="Verdana" size="1"><br>
                    <b><br>
                    One of the most awaited events on the corporate calendar of 
                    Oman, the OER CEO Golf, is going to be loaded with fun and 
                    surprises for both golfers and non-golfers</b><br>
                    <br>
                    <img border="0" src="images/ceo-golf.gif" width="250" height="240" align="right" hspace="9" vspace="5">The 
                    most anticipated corporate event in Oman – OER CEO Golf – 
                    the networking event of the year, will be played on Oman’s 
                    premier green golfing venue – the 18-hole Par 72 PGA-standard 
                    championship golf course at the Muscat Hills Golf &amp; Country 
                    Club on March 11.<br>
                    <br>
                    Much is happening in the business world here, and Thursday 
                    golfing, CEO golfing – as the event is now referred to by 
                    participants, is more than just golf. The OER CEO Golf event 
                    has been, traditionally, the unique networking opportunity 
                    every year for the crème-de-la-crème of Oman’s <br>
                    corporate society.<br>
                    <br>
                    This year too, the day-long event is designed to provide much 
                    nail-biting action for CEO golfers. The main competition is 
                    an 18-hole affair on a golf course that has been delicately 
                    crafted around the natural Omani mountain ranges to produce 
                    the first course of its kind in the Middle East. Players in 
                    this competition will be able to experience, for the first 
                    time in Muscat, what it feels to compete on a course designed 
                    by internationally renowned golf course designer Paul Thomas 
                    of Dave Thomas Associates, and constructed by Southern Golf 
                    UK. The word has spread around, and the corporate world is 
                    excited and abuzz about the 6th edition of OER CEO Golf. “OER 
                    CEO GOLF has always been an exclusive event. It is Oman’s 
                    biggest &amp; unique golfing-cum-business networking extravaganza,” 
                    says Sandeep Sehgal, CEO, United Media Services, the publishers 
                    of OER, Oman’s leading business magazine. “For us, it’s exciting 
                    that Oman’s most awaited corporate golfing championship will 
                    see everybody in the ‘who’s-who-of-Oman’ out there together 
                    on the greens.”<br>
                    <br>
                    <img border="0" src="images/ceo-golf0.gif" width="120" height="236" align="left" hspace="9" vspace="5"><b>Nawras 
                    business solutions is Corporate Presenter of OER CEO GOLF 
                    2010<br>
                    </b>Speaking about Nawras’ decision to be the official presenters 
                    of OER CEO GOLF last year, Nawras CEO, Ross Cormack said, 
                    “On this occasion we are delighted to sponsor the 2009 OER 
                    CEO Golf event which provides the opportunity for many senior 
                    business people to gather and network in a less formal environment 
                    than they might usually meet in.” About the day’s programme, 
                    Cormack said, “The OER CEO Golf day has become more and more 
                    popular with each passing year and this time promises to be 
                    the best so far with Nawras Business Solutions sponsoring 
                    the interesting and varied programme from dawn to dusk.”<br>
                    <br>
                    Oman’s customer-friendly mobile communications service provider, 
                    Nawras, is committed to enhancing people’s daily lives, making 
                    life more versatile and always a pleasingly different experience. 
                    For real corporate mobility, and enhancing business efficiency, 
                    Nawras Business Solutions has been the right choice for many 
                    organisations.<br>
                    <br>
                    With their vision to enrich the lives of people in Oman, Nawras 
                    constantly strives to be caring, excellent and pleasing, striving 
                    to build close relationships. They believe that people should 
                    have the time to focus on the important things in life. For 
                    this reason, they are always on the lookout for avenues that 
                    significantly enhance the quality of people’s lives. Considering 
                    their corporate ethos, it is not difficult to understand why 
                    they have agreed to be the Corporate Presenters of the 6th 
                    Edition of OER CEO GOLF too.<br>
                    <br>
                    <b>Rolex from Khimji’s Watches will also be partnering OER 
                    CEO Golf 2010.<br>
                    </b>Green is not the only unique facet about this year’s OER 
                    CEO Golf Apart from the 18-hole main event, Water Golf is 
                    on the cards. It’s an exciting event for golfers, golfers-to-be 
                    and their spouses. At the Serenity Lake at Muscat Hills, players 
                    will have to study the positions of a floating green, and 
                    aim to get their golf ball onto it. Got it? Great! Missed? 
                    Try retrieving your golf ball from the lake bed. Yes, there’s 
                    much fun even for golfers-to-be this year.<br>
                    <br>
                    Highlights of the day’s programme include a golf clinic for 
                    golfers-to-be over a four-hole round, conducted by golf professionals. 
                    <br>
                    <br>
                    For golfers-to-be, OER CEO Golf presents an exciting opportunity 
                    to be on the greens for a whole day, with no hefty charges 
                    to be paid!<br>
                    <br>
                    New, exciting and fun-filled activities have been planned 
                    to fill the day’s calendar, ending with the grand finale at 
                    the awards dinner – an evening that’ll be loaded with enthralling 
                    performances and mesmerising music. “This is the opportunity 
                    for real top-flight brands to be associated with a high-profile 
                    event in Oman. It’s become the most anticipated up-market 
                    event of the year in Oman, and every year, we try to make 
                    it bigger, more exciting. Throughout the day, and well into 
                    the night, it is going to be the best anyone has seen here 
                    in a long time,” says Alpana Roy, executive vice president 
                    of UMS. <br>
