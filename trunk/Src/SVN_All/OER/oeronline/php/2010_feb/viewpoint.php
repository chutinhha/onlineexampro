				<br>
                These eight goals are interlinked to form a wheel of performance 
                objectives. Their importance varies from sector to sector, 
                country to country and company to company. But they all merit 
                attention and, typically, progress on all of them is required to 
                optimise a company’s position.<br>
                <br>
                <b>Steering the future</b><br>
                    A growing number of indications suggest that the economy is 
                    coming back to health. But these indications also suggest 
                    that the recovery is likely to be gradual. The market will 
                    remain difficult and competition will be intense long into 
                    the future. Wishing recovery to be the case is not sufficient. 
                    Now is the time for management to take action. The performance 
                    wheel will provide a structured framework to set a new agenda 
                    and priorities. Each sector – and indeed company – will have 
                    a different set of priorities. But by considering own priorities 
                    and looking to understand how others are responding, companies 
                    will be well-positioned to steer their future. <br>
