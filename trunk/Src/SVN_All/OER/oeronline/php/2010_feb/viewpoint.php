                    </font><font size="2" face="Verdana"><b>Lessons from change</b></font><font face="Verdana" size="1"><br>
                <br>
                <b>A new performance agenda is emerging as companies respond to 
                the downturn and prepare for a different future. Not all of 
                these lessons from change may be new. But they are taking on new 
                importance as companies strive not only to survive, but to 
                thrive<br>
                </b><br>
                <br>
                The world is still reeling from the biggest financial downturn 
                since the 1930s. Trillions of dollars have been lost, millions 
                of people have become unemployed and the global economy has all 
                but shuddered to a halt.<br>
                <br>
                During the last two years, the seemingly infinite pool of global 
                funding that delivered a decade of cheap and abundant credit has 
                dried up. Economic players dependent on this credit — consumers, 
                companies and governments — have had to make rapid and radical 
                adjustments. Some have not survived. Some still struggle. And 
                some have emerged as new leaders. But all have had to adjust. If 
                we are to avoid repeating the mistakes that have caused such 
                economic distress, we need to learn from the past as we build 
                for the future. It has often been said that the most important 
                lesson to master is the ability to learn. Learning from others 
                is lower cost and lower risk than learning from experience. <br>
                <br>
                Ernst &amp; Young has continued to gather deep insights from 
                companies in many industry sectors and markets around the world 
                on how they are managing in this downturn. We have pulled 
                together these insights and tested our findings through an 
                in-depth survey of over 500 Ernst &amp; Young client service 
                partners from our member firms across the globe and additional 
                research by our Global Industry Centres. Combining all that we 
                have learned gives us a unique perspective into how companies 
                are responding to the new environment. <br>
                In this article, we look across these analyses to try to 
                establish a broader agenda for action that will be relevant for 
                all business — in every sector, in every country. As a 
                consequence, we believe a new performance agenda is emerging as 
                companies respond to the downturn and prepare for a different 
                future. Not all of these lessons from change may be new. But 
                they are taking on new importance as companies strive not only 
                to survive, but to thrive. <br>
                <br>
                <b>Back from the brink<br>
                </b>From the crash of Lehman Brothers in September 2008 through 
                to late February 2009, there was a real sense of panic across 
                the world. Uncertainty as to the financial strength – or 
                weakness – of a company extended from the boardroom to its 
                customers, suppliers and shareholders. Nightmare scenarios were 
                discussed. The economic world, as we knew it, looked over the 
                edge – and stepped back. Through bold government action and 
                massive, multifaceted stimulus bills, most of the major world 
                economies have modestly recovered from their abysmal 
                performance. Market indices have risen and trade measures have 
                begun to grow again. And most measures of consumer, producer and 
                executive sentiment have also begun to turn around.<br>
                <br>
                … but not to normal<br>
                <br>
                However, there is a sense that the future economy may be 
                different from the past. Demand may reappear, but it is likely 
                to be depressed. The reappearance and continuation of high 
                levels of unemployment, the prospect of increased taxes to fund 
                the deficits and further restrictions on consumer credit will 
                act to dampen consumer confidence and increase price 
                sensitivity.<br>
                <br>
                As stimulus measures are gradually withdrawn a period of public 
                spending contraction in real terms can be expected for many 
                economies. Extensive losses to both investors and the public 
                purse renew pressure on the corporate governance approach that 
                has seemed to deliver neither transparency nor protection for 
                wider interests. At the same time, the regulatory burden is 
                expected to rise: partially to mitigate the role business plays 
                in damaging the environment and partially to respond to failures 
                in the market. These failures include risk management programmes 
                that neglected to spot risk and remuneration programmmes that 
                rewarded people where businesses have failed.<br>
                <br>
                <b>prepare for rebound<br>
                </b>Depressed demand and additional burdens make for a highly 
                cost-sensitive environment. Many companies expect their access 
                to additional funding to remain constrained, as banks act to 
                recapitalise and the market for fundraising remains largely 
                tepid. As a result, we are seeing major program of cost 
                reduction and restructuring in many companies across most 
                sectors in an attempt to free up working capital and improve 
                performance efficiencies.<br>
                <br>
                On the upside, many executives believe there are major 
                opportunities to make a step-change transformation to markets 
                and competitive positions. This has been a global recession and 
                it will be a globalised recovery. The structure of many sectors 
                may be open to a fundamental change in a way not seen for 
                decades. Similarly, it is widely felt that many of the largest 
                “emerging” markets, particularly China, India and Brazil, have 
                finally emerged into the global economy and are likely to 
                perform more strongly than many of the “mature“ economies.<br>
