            <img border="0" src="cov4.jpg" align="left" hspace="5" width="250" height="128">An 
            active private sector is spurring the Sultanate’s construction 
            industry forward with mega tourism and infrastructure development 
            projects while the government too is pushing ahead with much-awaited 
            developments, writes Janaki Raghavan</b><br>
            <br>
            </font><font face="Verdana" size="3"><b>N</b></font><font face="Verdana" size="1">ot 
            too long ago, economists had predicted that growth would level out 
            as the demand for further construction slows down. However, the 
            Middle East, defying all of this and the global downturn, is forging 
            ahead with multi-billion-dollar projects. The GCC construction 
            spending in major projects is estimated to have crossed Dh230 
            billion ($68.85 billion) with Dubai attracting 67.61 per cent of the 
            total, according to a recent report by HSBC Bank. <br>
            <br>
            This includes $42.5 billion worth of investment in development and 
            construction undertaken in Dubai. The rest was invested in the five 
            neighbouring GCC countries. That the region's construction industry 
            is booming, with big-ticket construction projects on the anvil is 
            evident in the fact that investment in the power sector alone is set 
            to hit $200 billion by 2020. High liquidity, property sector reforms 
            and improving geo-political conditions have been identified as the 
            key drivers of the construction boom in the region. Record oil 
            prices, funds repatriation post September 11, and improved 
            geopolitical conditions in the region have created significant 
            liquidity in the GCC.<br>
            <br>
            “The increase in liquidity, together with the willingness of the GCC 
            governments to diversify their economies, has created a construction 
            boom,&quot; says an industry observer. Furthermore, property sector 
            deregulation in the region has been met by the private sector with 
            considerable enthusiasm. Industry estimates range from $30 billion 
            to $45 billion for planned real estate and related construction 
            spend over the next seven years.<br>
            <br>
            <b>Oman forging ahead</b><br>
            Plush residential complexes, luxurious resorts, green golf courses 
            and mega infrastructure projects – they are the hallmarks of the new 
            construction boom in the Gulf, and Oman, with its massive projects, 
            is but too keen to join the race. Although Oman may not be in the 
            same league as Saudi or UAE, there is no denying that the 
            construction sector here too is making impressive strides, logging a 
            whopping 13 per cent growth in 2003.<br>
            <br>
            The numerous mega projects under construction and on the drawing 
            boards are making industry watchers sit up and take note of the 
            Sultanate’s ambitions. Approximately 10 big players, 35-40 medium 
            firms, and 70-80 small firms share the country’s construction 
            market. Most big players have already had operations in other 
            countries in gulf, most prominently in UAE and Qatar. This strategy 
            does provide more stability and a larger canvas to operate. 
            Unprecedented growth in residential and commercial construction in 
            Dubai has left most local companies fully stretched making the 
            scenario attractive for large Omani companies to participate in the 
            great Dubai construction boom. <br>
            <br>
            The industry, which was floundering during 2000-02, received a fresh 
            lease of life when the government drew up new infrastructure plans 
            to give succour to the ailing industry. <br>
            <br>
            The country has been witnessing frenetic construction activity 
            during the past couple of years, and the share of construction to 
            the GDP is expected to touch 5 per cent by the end of this year from 
            the current 3-4 per cent. The thrust on tourism and the consequent 
            spate of tourism projects in an effort to diversify from oil-based 
            income have also acted as a catalyst for the construction industry. 
            Oman is poised to receive 1.23 m visitors annually in the next 10 
            years. The visitor spending is expected to be $1.2 billion and 
            estimated new construction is $2.060 billion during this period.<br>
            <br>
            The massive investments in projects that aim to increase oil output 
            and tap its gas reserves have also contributed significantly to the 
            turnaround of the industry’s fortunes. In sync with the region-wide 
            high demand for new housing, Oman’s housing segment also shows a 
            promise of growth over the next 10-15 years given the country’s huge 
            population of youth. The decision by the authorities to allow 
            expatriates to buy property is also expected to catalyse the housing 
            construction boom. <br>
            <br>
            With two housing banks actively providing finance to low and middle 
            income groups, real estate investments are keeping pace with the 
            growth in the economy. The government allocation for public housing 
            in the current (2001-05) five-year plan is RO16.2 million.<br>
            <br>
            <b>Riding the tourism wave </b>
            <img border="0" src="wav.jpg" align="right" hspace="5" width="200" height="130"><br>
            Stretching along 7.3 km of beachfront just west of Muscat and 
            occupying 195 hectares, the $805 million (RO310mn) resort project – 
            The Wave – is ready for development. The Wave, which includes a 
            cluster of hotels, chalets, and a golf course in addition to 505 
            residential units, will be a fully integrated premium tourism and 
            residential resort. For the first stage, a projected total of RO32 
            million will be invested to reclaim 400,000 square metres of land, 
            develop the infrastructure and build a golf course, marina and yacht 
            club.<br>
            <br>
            The ambitious $170m Barr Al Jissah Resort and Spa project of Oman is 
            currently on schedule with an anticipated official launch by 
            November 2005. Located on the Gulf of Oman, a $308 million heritage 
            resort is being developed along 12 km of beaches in the Sawadi area 
            of Barka by an Omani-European consortium of Al Sawadi Development 
            and Investment Company and the German Tourism Organisation for 
            Development and Investment. The first phase of the complex, will 
            include a number of four-star and five-star hotels, chalets, 
            restaurants, golf courses and other entertainment facilities.<br>
            <br>
            <img border="0" src="soh.jpg" align="left" width="200" height="132"><b>The action is in Sohar</b><br>
            The accent on diversification from oil has put the industrialisation 
            drive on the fast track and Oman has a slew of industries related 
            construction projects in the pipeline. While 2 large projects, Oman 
            India Fertilizer Complex and Qalhat LNG – Train 3 are approaching 
            completion by mid 2005, the scene of action has already moved to 
            Sohar. Having started with Sohar port, a large sea water intake and 
            return system project and Sohar IWPP, the region is ready for the 
            construction of petrochemical plants, aluminum smelter, fertilizer 
            complex etc. Massive road projects like Southern express highway, 
            sewage and water distribution networks are providing the right 
            impetus to the construction sector growth momentum.<br>
            <br>
            The Sohar refinery, which will have a capital expenditure of $ 1.2b, 
            has reached the implementation stage and is expected to be 
            operational in early 2006. The construction of a $2m dollar 
            aluminium smelter project is on schedule and production is set to 
            start in 2007. <br>
            <br>
            The Sohar refinery, which will have a capital expenditure of $1.2b, 
            has reached the implementation stage and is expected to be 
            operational in early 2006. The construction of a $2m dollar 
            aluminium smelter project is on schedule and production is set to 
            start in 2007. <br>
            <br>
            The Government of the Sultanate of Oman, Oman Oil Company (OOC) and 
            The Dow Chemical Company (DOW) have announced an agreement recently 
            to form a joint venture that will design, build and operate a 
            petrochemical complex in Oman. Located in the Sohar Industrial Port 
            Area, the petrochemical complex will comprise feedstock production 
            facilities, a gas cracker, as well as three world-scale Polyethylene 
            production units based on state-of-the-art catalyst and process 
            technology. <br>
            <br>
            Oman has signed a $312-million financing deal with several banks to 
            help fund the building of a polypropylene plant. South Korea’s LG 
            International and LG Engineering have been awarded a RO70-million 
            ($181.9 million) contract to build the 340,000 tonnes-per-year 
            plant. The Oman Polypropylene joint venture, which was set up in 
            2002, is expected to be completed in 2006.<br>
            <br>
            The government has signed another $300 million shareholders' 
            agreement with LG International (LGI) of South Korea and Iran's 
            National Petrochemical Company (NPC) for setting up an ethylene 
            dichloride (EDC) unit in Sohar, which will begin commercial 
            production in the second quarter of 2008.<br>
            <br>
            A major area of focus has been the development of gas-based projects 
            to advance the development of the government’s gas resource. 
            Significant progress has also been made in the development of large 
            gas-based industries. <br>
            <br>
            Some of the big players in the market like Bahwan Engineering Co. / 
            Bahwan Contracting Co., Carillion Alawi and L&amp;T Oman are already 
            busy with healthy order books and well prepared to rise to the 
            challenge of construction boom in Oman. <br>
            <br>
            With regard to ports, a flurry of activity is also taking place in 
            the multi-purpose port and dry dock project planned at Duqm in 
            Wusta, the Khasab and Kumzar ports and the other ports. Two 
            additional berths are being planned in the Salalah port. The Sohar 
            industrial port will primarily be used for bringing raw material for 
            heavy industries and for exporting finished goods especially for the 
            heavy industries coming up in the area. <br>
            <br>
            The other big-ticket projects in the pipeline include the RO100m 
            palace in Muscat, health centres for the Ministry of Health and a 
            shopping mall in Sohar.<br>
            <br>
            <b>Omanisation drive</b><br>
            The construction sector being one of the largest employer (estimated 
            at 150,000 people) provides excellent opportunity for young Omani 
            Engineers, technicians and field workmen. Currently, the Omanisation 
            target is 25% for the year 2005, increasing by 2.5% each year. 
            Omanisation in construction sector has seen a focused and concerted 
            approach owing to various initiatives. The challenge, however is to 
            inculcate work ethic, technical competence and retention to derive 
            full benefits of Omanisation<br>
            <br>
            <b>Combating challenges</b><br>
            However, a few thorny issues are raising the hackles of the 
            construction firms. The current construction boom in the Gulf has 
            led to greatly increased demand for materials - especially steel and 
            cement - and that, in turn, has caused prices to skyrocket. Between 
            November 2003 and April 2004, for instance, UAE steel prices doubled 
            to nearly $540 per ton; during the same period, prices for cement 
            rose by 50 per cent. <br>
            <br>
            “The steep hike in the steel and cement prices has had disastrous 
            effects on the bottom line of companies. Most of the projects were 
            tendered before the prices shot up and so profits of builders have 
            taken a beating,” says an Industry observer.<br>
            <br>
            It is, however, heartening to note that builders have not resorted 
            to compromising on the quality of construction to safeguard profit 
            margins. “The quality of construction is excellent in Oman and is 
            undoubtedly at par with international standards,” asserts a leading 
            player. Despite the hiccups, the mood in general seems to be upbeat. 
            The upswing will continue for the next 4-5 years, and the players 
            will remain busy say insiders. </font></p>
