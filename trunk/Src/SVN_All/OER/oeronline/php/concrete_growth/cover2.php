            productivity, has reached the stage of trial run. Cranium is a video 
            system designed to improve productivity and safety of crane 
            operations by improving communications. A video camera mounted on 
            the crane bottom transmits an image to the television monitor in the 
            crane cab. The operator has a real-time picture of the loads and 
            craftsmen that might otherwise be out of the direct line of sight.
