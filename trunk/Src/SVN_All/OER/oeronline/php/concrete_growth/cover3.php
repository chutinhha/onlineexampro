            stages of planning and implementation in different parts of the 
            country. <br>
            <br>
            Unlike many other countries, the demand for excavators is more in 
            the Sultanate in view of the peculiar topography of the country, 
            which consists of hilly terrain and very hard rocks. This calls for 
            an excavator-hammer combination to break mountains without 
            explosives for initial civil work. <br>
            <br>
            <img border="0" src="cov8.jpg" align="right" hspace="5" width="140" height="203">Although Omani market is relatively small in terms of sales volume, 
            all major global players are actively competing to enhance their 
            slice of the market. The major equipment players include 
            Caterpillar, JCB, Komatsu, Volvo, Hitachi, Xcmg . The vehicles &amp; 
            tippers are by Hino, Man and Mercedez-Benz &amp; Steyr. The products of 
            these companies include bulldozer, motor grader, hydraulic 
            excavator, wheel loaders wheeled excavators, compactors. The 
            authorised dealers believe that the volumes are likely to improve 
            further once some of the mega projects, like The Wave, reach the 
            implementation stage, which is expected within a year. However, the 
            demand for mining equipment (large earthmoving equipment) is 
            relatively low compared to construction equipment in the country, 
            mainly due to the fact that mining is yet to assume large 
            proportion. Presently, few cement manufacturers are carrying out 
            mining activity for their raw material requirements. <br>
            <br>
            It may be noted that the multinational companies that bagged 
            projects in Oman prefer to hire earthmoving equipment rather than to 
            go for outright purchase. This has resulted in a further thrust to 
            machine hiring business. Entrepreneurs, who invest relatively 
            moderate amount, can buy these machines with the help of finance 
            companies with the intention of hiring it to large contracting 
            companies. This phenomenon has also resulted in better demand for 
            lease and hire purchase businesses. To avoid risk, it is preferable 
            to maintain a few machines than a large fleet with the company. This 
            would not only enhance efficiency in utilization of machines but 
            also create gainful employment opportunities for Omani nationals.
            <br>
            <br>
            The important aspect for marketing earthmoving equipment includes 
            the quality of the machine. With an equal importance given to dealer 
            after sales support and machine availability being the two most 
            important aspects as lack of service support could delay projects, 
            leading to huge losses to the companies. Reliability and ease of 
            operation are other key ingredients.<br>
            <br>
