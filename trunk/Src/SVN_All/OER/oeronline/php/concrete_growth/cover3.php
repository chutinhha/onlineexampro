            100 per cent growth last year and the trend is likely to continue in 
            the next three to four years. This is in view of a phenomenal growth 
            in infrastructure, industrial and tourism ventures, including the 
            Barr al Jissha Resort, Sohar Aluminium, The Wave, Sohar Port 
            expansion, Sohar Refinery, a portion of Quriyat-Sur road and the 
