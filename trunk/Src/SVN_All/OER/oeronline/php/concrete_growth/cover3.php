            and credit terms and ignore maintenance cost or the productivity of 
            the machine. This clearly indicates the customer ignorance about the 
            maintenance cost of the machine. In fact, the customers do not 
            attach any importance to the hidden cost, which is a significant 
            factor in determining the cost of operation over a period of time. 
            It is also important to know the productivity of the machine in 
            terms of either ton or cubic metres of earth moved for rial Omani 
            for either out right purchase of a machine or hiring a machine. The 
            purchaser should estimate operating and owning cost of the machine 
            before going for outright purchase or hiring a machine. Like other 
            developed markets, the local companies will have to take into 
            account productivity of the machine rather than merely considering 
            the price and credit extended by finance companies or suppliers. In 
            this connection, the dealers will be in a better position to brief 
            potential customers on operating cost of each machine, which would 
            help them to choose the right equipment. With the marketing showing 
            remarkable growth, these aspects will be crucial factors in the 
            coming days. <br>
