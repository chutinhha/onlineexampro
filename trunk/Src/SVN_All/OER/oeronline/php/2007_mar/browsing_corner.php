		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><font face="Verdana"><font style="font-size: 14pt"><b>Follow the 
		Leader</b><br>
        </font><b><font size="1">Two giants in the financial world, Donald Trump 
		and Robert Kiyosaki, team up to explain how to make money and become 
		financially competent in a world of economic uncertainty</font></b><font style="font-size: 14pt"><br>
		<br>
        </font><font size="1">
		<img border="0" src="browse.jpg" width="150" height="219" align="left" hspace="6" vspace="4">If 
		you think that the world’s escalating oil prices, the value of the 
		dollar and growing trade deficit are not your concern, think again! Be a 
		part of the solution, not the problem, say business heavyweights, Donald 
		Trump and Robert Kiyosaki, as they introduce you to a wealth of 
		information on how to make money by getting attuned to the economic 
		climate, keeping the learning process alive and, above all, having 
		vision.<br>
		<br>
		In this succinctly written book, Why We Want You To Be Rich, Trump and 
		Kiyosaki examine the pitfalls of the shrinking middle class, and address 
		the issues causing the rich to get richer and the poor to get poorer. 
		The widening gap between the rich and the poor is bound to affect 
		everyone in the long run unless there is a paradigm shift. Stop thinking 
		small and you will automatically widen your quest for money, reiterate 
		the business gurus. They also mentor their readers on the merits of 
		amassing wealth as a means of increasing power and insulating oneself 
		financially in the future.<br>
		<br>
		The book elaborates on three levels of financial advice: advice for the 
		poor, advice for the middle class and advice for the rich. The dictum of 
		the financial experts is to get out of the habit of waiting for savings 
		or entitlements every time. Don’t be passive investors working hard for 
		your money. Instead, expand the vistas of your mind and let your money 
		work for you by becoming active investors. Stop thinking that you 
		weren’t born with a silver spoon or that you haven’t had the greatest 
		education. Focus on leveraging your opportunities and enhance the 
		quality of your life. According to the authors, everyone is born rich 
		since the most important trump card one possesses is the mind. This book 
		emphasises on the ability to do more with less by thinking big and the 
		advantages of becoming disciplined investors who can gain much higher 
		returns with lesser risk.<br>
		<br>
		So, what is the best means of investing? Is there a universal key that 
		can unlock the treasure chest? The answer is no. This is not a 
		one-size-fits-all kind of situation. What this book doesn’t answer is 
		specific queries on what to do with your pot of money. It equips you, 
		instead, with financial education and preparation to give you generic 
		information on how to become more money savvy keeping different target 
		groups in mind.<br>
		<br>
		Embark on your journey for money by following Trump and Kiyosaki’s 
		step-by-step process of leverage, control, creativity, expansion and 
		predictability. Go back in time and journal the key learnings of your 
		life. Use these crucial ‘brainwave’ moments and integrate them with 
		focus and commitment to become richer, recommend the authors. So, follow 
		their tips on investing in real estate, network marketing or 
		entrepreneurship. Then, go find your own magic formula to make money.</font></font></p>
