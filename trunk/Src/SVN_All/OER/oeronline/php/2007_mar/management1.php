<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font size="4" face="Verdana">Brand it like Beckham <br>
Can the soccer star sustain the hype?</font></b><font face="Verdana" size="1"><br>
<b>Beckham has been tapped to boost a team’s popularity and success, a trend not 
unlike when a corporate board hires a high-profile CEO to rescue a failing 
company</b></font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b><br>
<img border="0" src="manag3.jpg" width="300" height="201" align="left" hspace="6" vspace="4"></b>In the 2002 British movie, Bend It Like Beckham, an aspiring 18-year-old soccer 
player explains to a friend that “No one can bend it like Beckham.” It’s a 
reference to the international soccer star David Beckham’s amazing ability to 
score by ‘bending’ the ball, and making it swerve as it flies through the air.<br>
<br>
With the recent announcement that Beckham has signed a landmark sports contract 
worth an estimated $250 million to play soccer with the Los Angeles Galaxy over 
a five-year period, Beckham’s knack for bending conventional approaches to 
soccer, business and his own lifestyle appears remarkably intact. Not only is he 
considered one of the world’s best-known soccer players, he’s a recognized 
global brand, with lucrative celebrity endorsements that have included Adidas, 
Gillette, Got Milk?, Pepsi and Motorola. His marriage to former Spice Girl 
Victoria ‘Posh’ Beckham, coupled with his highly publicized friendships with 
movie stars like Tom Cruise and Katie Holmes, have only added to the allure.<br>
<br>
But what some Wharton marketing and sports experts wonder is whether Beckham can 
live up to the hype surrounding the deal and his move from Spain’s Real Madrid 
soccer team to the Los Angeles Galaxy. Can Beckham, whom some consider to be in 
the twilight of a brilliant career, produce enough star power to not only boost 
the team’s revenue but also raise the profile of Major League Soccer (MLS) in 
the United States?<br>
“It’s risky. But there’s always a risk when ... everything is centred on the 
brand of one person,” says Wharton marketing professor Lisa Bolton. “But the 
thing about Beckham is that he can cut through the clutter. The team doesn’t 
have to feature him with its own advertisements in order to raise awareness of 
soccer.”<br>
<br>
Kenneth L. Shropshire, director of the Wharton Sports Business Initiative, notes 
that Beckham’s contract is one of the biggest in North American sports. It 
catapults him into a rarefied league with athletic stars like golfer Tiger 
Woods, who makes a reported $100 million a year. Beckham’s $50 million-a-year 
contract from a combination of salary, franchise royalties, advertisements and 
endorsements puts him ahead of basketball legends Michael Jordan and Shaquille 
O’Neal; football quarterbacks Peyton Manning, Michael Vick and Matt Hasselbeck; 
baseball’s Alex Rodriguez; and seven-time Tour de France winner Lance Armstrong.<br>
<br>
<b>Extra 15 minutes of fame</b><br>
<img border="0" src="manag4.jpg" width="200" height="350" align="right" hspace="6" vspace="4">In this blending of sports and entertainment, Beckham, who played for England’s 
Manchester United until he moved to Spain in July 2003, more than stands out. 
“Part of what makes Beckham a unique brand is that he is one of the few sports 
icons who combines this whole package of a glitzy and glamorous fantasy world – 
a good athlete, Hollywood good looks, a good-looking wife,” says Wharton 
marketing professor Americus Reed II. “He started building the brand in the 
context of the world’s most popular sport. Moving his brand to the United States 
gives him the opportunity to extend his 15 minutes.”<br>
<br>
Yet Beckham as brand is also risky. In what many observers are calling “star 
economics,” Beckham has been tapped to boost a team’s popularity and success, a 
trend not unlike when a corporate board hires a high-profile CEO to rescue a 
failing company. But a new CEO rarely carries expectations of rescuing an entire 
industry. Beckham’s assignment is not only to increase the profile of a team but 
to push the sport of soccer to new levels in a country where it remains a 
distant cousin to other sports. The tactic has been tried before, and failed. An 
international soccer star like Pele couldn’t save the New York Cosmos in the 
1970s, and star European soccer imports couldn’t save the North American Soccer 
League before it declared bankruptcy in 1984.<br>
<br>
Seeking to avoid these mistakes since it was formed in 1996, the MLS has 
carefully controlled expenses and player salaries within its 13 league teams – 
until now. Last year, the league changed its salary rules to allow each team to 
sign a player outside the salary cap, a move now being referred to as “the 
Beckham rule.” And in a deal that the Anschutz Entertainment Group (AEG), which 
owns operating rights to the Galaxy and two other MLS teams, has described as 
“the biggest in sporting history,” Beckham’s five-year pact even includes a 
percentage of team jersey sales and ticket revenue.<br>
<br>
<b>Does scoring goals matter?</b><br>
Will Beckham’s star power decline if he doesn’t play well in the United States? 
After all, he didn’t start for the majority of this season with Real Madrid, and 
the coach benched him after he announced his decision to join the Galaxy. Bolton 
and Wharton marketing professor David J. Reibstein argue that Beckham’s appeal 
isn’t dependent on his scoring goals. “This is sports as entertainment,” says 
Bolton. Adds Reibstein: “The issue is, we have a way to market the sport.”<br>
<br>
Yet sports author and journalist Michael Sokolove, who recently published “The 
Ticket Out: Darryl Strawberry and the Boys of Crenshaw,” cautions that American 
sports fans shouldn’t be underestimated. “We think that sometimes it’s all about 
marketing,” says Sokolove. “But American sports fans are really attuned to 
performance. They are smart enough to know that Beckham is fabulous and a global 
brand, but he is coming to the United States when his career is somewhat on the 
downside. No one has ever been a successful marketer and built up their sport 
unless they really delivered the goods on the field.”<br>
