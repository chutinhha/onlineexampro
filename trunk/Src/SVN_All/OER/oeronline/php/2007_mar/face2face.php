<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Taking the Battle into Enemy Territory</font><br>
</b><font face="Verdana" size="1"><b>Keith Scott, General Manager, Alliance Housing Bank SAOG, talks about the bank’s 
foray into commercial banking, and the turbulent market conditions in the 
mortgage market, in an interview with Akshay Bhatnagar</b><br>
<br>
<b>
<img border="0" src="face.jpg" width="150" height="182" align="left" hspace="6" vspace="4">From being a mortgage player, Alliance Housing Bank (AHB) is becoming a 
commercial bank. When is this likely to be completed?</b><br>
We have received in-principle approval for commercial banking from the Central 
Bank of Oman (CBO). KPMG has been commissioned to ascertain the viability of the 
proposal. They are conducting the study and we expect them to submit their 
report in March.<br>
&nbsp;Thereafter, the business plan will be shared with our 
shareholders and the Central Bank of Oman for their approval before we move 
forward.</font></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>What were the factors that guided AHB in taking the plunge?</b><br>
The banking sector in Oman is performing strongly and we think that there is a 
lot of growth potential in the market. Secondly, CBO has allowed commercial 
banks to lend 5 per cent of their total portfolio for housing purposes. This has 
resulted in commercial banks making a strong entry in the mortgage business, the 
segment in which we operate. Though we successfully met the fierce competition, 
we thought why should we not get into their territory by becoming a commercial 
bank! As a successful housing bank, we have achieved a very high level of 
customer service standards and we think our customer-focused approach can lead 
us to replicate our success in commercial banking also.<br>
<br>
<b>Your net profits were down by 11 per cent in 2006 compared to 2005. So growing 
competition did dent your bottom-line?</b><br>
We shouldn’t forget that CBO’s relaxation for commercial banks had immediately 
made available RO200 million in potential lending in the housing loan market. 
But we thrived in the turbulent market, as our loan book went up by 13 per cent 
and interest income also increased by 14 per cent in 2006.<br>
<br>
Our third quarter loan growth was quite limited as one bank in particular was 
aggressively targeting our customers. But by the fourth quarter, we really got 
our act together. We arrested loan pre-payments and our loan growth was very 
strong. Despite a difficult time, our sanctions were at a record level last 
year. We made major investments in infrastructure development to further raise 
customer service standards. So, due to these investments and competition 
factors, our average margin declined. This year, we will show a lot of 
improvement in our performance.</font></p>
<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
