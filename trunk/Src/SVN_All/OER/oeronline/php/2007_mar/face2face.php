<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>What makes Oman such a hot property among mortgage players?</b><br>
It is a very safe market to be in and I can vouch for it. Virtually all our 
loans have double security. We have a mortgage that is enforceable. In other 
jurisdictions regionally, it is not always possible to enforce the mortgages. 
Most of our customers are employees of the government or highly reputed 
organisations, and through a salary assignment, our repayments are also secured.<br>
<br>
Also, the value of real estate assets is going up rapidly. That is good for the 
lender also as the value of security is going up. So month-by-month, the 
lender’s risk is reducing. I expect the real estate market to continue 
appreciating in the coming years.<br>
<br>
<b>Going by your ad campaign ‘Myth vs Reality’, does it mean that a high degree of 
potential customers are still averse to taking a housing loan?</b><br>
Our marketing communication based on the ‘Myth Vs Reality’ concept has been very 
successful and has delivered good results. The general mindset is definitely 
changing, as more residential units are being bought or developed. The way 
people live is changing dramatically. Our recent experience says that a section 
of the younger generation that is earning good money is more inclined to have a 
house of their own rather than living with the family. In a way, increased 
competition has also helped in expanding the market, with the other banks too 
bombarding their marketing communication, promoting housing loans.<br>
<br>
<b>Do you think the average age of your customers has come down?</b><br>
The average age of our customers has definitely come down. The bulk of our 
customers are in the 30-35 years age group. The reason for this is that older 
people are comparatively wealthier and many of them are clearing their loans or 
not borrowing any more. Against this, the younger people are making more 
purchases based on borrowings.<br>
<br>
<b>What is the share of Omani, GCC nationals and other expatriate customers in your 
loan book?</b><br>
Our loan offering to expatriates in designated areas has generated good 
response. But, so far, we have not disbursed loans to expatriates as they are 
paying their share first and we will start paying our share once the requisite 
regulations are in place. We do have GCC nationals as our customers but their 
numbers are very low, so our loan book of over RO150 million comprises mainly 
(almost 100 per cent) of Omanis.<br>
<br>
<b>How many applications do you receive, and how many of them are sanctioned and 
disbursed in a year?</b><br>
We get around 900 applications for loans in a year. Around 80 per cent of them 
are generally approved and 70 per cent or so end up in disbursal.<br>
<br>
<b>What’s the reason behind this gap between approvals and disbursals?</b><br>
Sometimes, the customers change their mind and decide not to go ahead with the 
loan. In some cases, the loan is approved for construction and it is disbursed 
over a period of a year or more. But in many cases, the customers are ‘shopping 
around’. After taking the offer from us, they go to a competitor and negotiate 
better terms.<br>
<br>
<b>How many potential customers approaching the AHB ‘shop around’?</b><br>
Most of them are ‘shopping around’. With more options due to increased 
competition, they are looking for the best option.<br>
<br>
<b>As a customer, if I already have a good existing relationship with a leading 
bank, why should I come to AHB? Why not go to my existing bank for a housing 
loan?</b><br>
That’s exactly the line the other banks are using in trying to persuade their 
customers to take up housing loans. Our USP is that we give the most competitive 
offer than any other bank in the Sultanate. Our interest rates tend to be the 
lowest and we believe our products are the best by virtually any measure...<br>
<br>
<b>What are your interest rates?</b><br>
Our base lending rate is 6.55 per cent. Other banks lend at 7 per cent and 
above, as does the rest of the competition. We calculate interest on reducing 
balance rather than flat rate, which means that the loan gets repaid faster 
resulting in fewer payments to be made by the customer. Some other banks charge 
insurance premium for the life of the loan upfront and add it to the loan. We 
don’t do that as this results in higher instalments for a customer. We do our 
business transparently.<br>
<br>
Housing loans are our business and we know that business better. We offer good 
customer service to make our customers comfortable and satisfied. We give 
tailor-made offers to suit the needs of our wide variety of customers. Our 
turnaround time for an application is five days whereas the competition takes 
much longer. We have an automated system in place to seamlessly speed up the 
process from the time the customer walks in to the time the loan is disbursed.<br>
<br>
<b>How you compete with commercial banks having large networks banks offering 
multiple products in the interiors?</b><br>
We are very conscious of this reality. That is why we have launched a mobile van 
service that travels around and promotes our services in locations where we are 
not represented. In the last two months, it has already covered around 40 
locations. Our direct sales team, call centre and service centres supports our 
strategy of reaching out to the customer. We also have the ability to accept 
applications online.
