<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Powered to Succeed</font></b><font face="Verdana" size="1"><br>
<b>Dr Ranald Spiers, the CEO of International Power Plc, is upbeat on Oman’s power 
sector reforms and is planning to install more power generation units in the 
country, reports Sunil Kumar Singh</b><br>
<br>
<img border="0" src="passby.jpg" width="150" height="186" align="right" hspace="6" vspace="4">Oman, along with Abu Dhabi, is showing the best examples in power sector 
reforms, says Dr Ranald Spiers, CEO (Middle-East and Africa) of International 
Power plc, a London-based independent power producer with its regional 
headquarters in Abu Dhabi. <br>
<br>
“I am very interested to do more in Oman and add more power generation 
capacities here. We’re very much pleased with the things the Sector Law has 
changed in Oman and it has been adopted by other countries as well,” Dr Spiers 
adds.<br>
<br>
International Power plc is currently operating its wholly owned subsidiary, Al 
Kamil Power Company SAOG, which owns the gas-based, 285-MW independent power 
project called Al Kamil power plant at Al Kamil in Oman’s eastern region. Spiers 
is also the chairman of the board of directors of Al Kamil Power Company, which 
reported a net profit of RO1.498 million last year. <br>
<br>
In 2000, after having won the contract to set up and operate the power station, 
International Power and the government entered into a power purchase agreement 
whereby the company agreed to generate power and sell it to Oman Power and Water 
Procurement Company for 15 years beginning 2003. Further, in 2004, the company 
offloaded 35 per cent of its stake to the public, retaining 65 per cent holding 
in accordance with original project agreement with the government.<br>
<br>
<b>Deliverables come first<br>
</b>At 51, Spiers is perhaps more active and energetic than most business executives 
of his age. Based in Abu Dhabi, he keeps on shuttling among the company’s 
regional operations located in Bahrain, Saudi Arabia, Qatar and UAE, besides 
Oman. His core responsibilities include the development, financing, construction 
and operation of power and water desalination assets in the Middle East and 
Africa. “All of our projects have been running trouble-free,” he replies with 
glint in his eye when asked about the status of other projects run by his 
company in GCC.<br>
<br>
Since 1992, he has had a long successful spell with National Power (as the 
company was known by that name before demerger in 2000). His responsibilities 
included focussing on business development and managing projects in Turkey, 
Italy, Holland, India, Zimbabwe, South Africa, Pakistan, Kazakhstan, and 
Middle-East. Prior to joining National Power, he served in the BP Group for 12 
years – including 4 years in the US – across a wide spectrum of industries, 
including advanced materials and aerospace, petrochemicals, detergents, natural 
gas and oil refining.<br>
<br>
<b>Balancing life<br>
</b>Spiers has a brilliant academic record. He is a BSc in Chemistry from Edinburgh 
University, an MBA in International Business from City University in London and 
a PhD in chemistry from Cambridge University. His demanding job as CEO of a big 
company keeps him busy nowadays. So busy in fact, that he longs for a leisure 
break. “I haven’t had much leisure time actually. Whatever free time I have, I 
try to go to gym, swimming. I also like to travel and go out to desert and enjoy 
the natural habitat. Music, reading, playing piano, watching rugby are my other 
pastimes. I like to spend time with my family but it is rare,” he candidly 
admits.<br>
<br>
Spiers, father of two, has been treading on a balanced path in both his personal 
as well as professional lives, and this balancing act is what makes all the 
difference between the achiever and also-rans. At the end of the day, the 
success International Power Plc has achieved in GCC, including Oman, reflects 
the resounding success of Oman’s power sector reforms too, a success which will 
repeat itself in the days to come. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
