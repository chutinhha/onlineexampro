<br>
Young Master<br>
<br>
Hassan Mohammed Juma, Managing Director, Mohammed Juma Sultan Co. LLC, has 
achieved what most of his peers could not even have imagined, yet he feels he is 
just a beginner, reports P Aneel Kumar<br>
<br>
<br>
At 33, he looks more a management guru than the managing director of a company. 
Talk to him, and his knowledge impresses you. You ask him about himself and he 
tells you he’s still a beginner. Meet Hassan Mohammed Juma, the Managing 
Director of Mohammed Juma Sultan Co. LLC. At his age, he has achieved what his 
peers could not have even imagined. But he is humble enough to attribute most of 
his success to his family, particularly his late father, and two of his former 
superiors at the start of his career. “My father was a self-made person who 
started from below zero. With his vision and hard work, he had become a very 
successful person. He never had any formal education, but was one of the most 
successful business persons in his generation. He used to import several 
products, mainly coffee, into Oman. In fact, he was better known as The Coffee 
Man of Oman. I believe it is his hard work and determination that put us here 
today,” says Juma. <br>
A graduate in science from Sultan Qaboos University, Juma started his career in 
National Bank of Oman at the age of 24. The two-year stint as a relationship 
manager in the corporate banking division acted as a foundation for his growth. 
“Two people at NBO were instrumental in my shaping up as a business person. They 
opened my eyes to the corporate world. They believed in me and motivated me to 
do greater things. Their guidance and training helped me immensely during later 
part of my professional career,” says Juma, who describes himself as a 
perfectionist.<br>
Learning curve<br>
With two years of experience under his belt, Juma moved to Majan International 
Bank as a head, e-banking. There he was in charge of credit cards, e-banking, 
tele banking, ATMs, etc. Juma didn’t find the job easy, but it gave him a lot of 
exposure to top management and also allowed him to be in contact with senior 
officials around the world. <br>
“It was a very challenging job as I was just two years old in the banking 
industry. As I had never worked at the ground level, I asked my top management 
to allow me to work in a small branch as a junior officer. I worked in a branch 
for 2-3 months doing all things a new recruit would do. I was pretty young then, 
so no one knew I was going to head e-banking. This short experience taught me 
many more things than my two-year stint in NBO.” adds Juma. <br>
Even while he was getting into the groove of his second job, Majan International 
Bank merged with Bank Dhofar. During this period, he joined Shell Marketing. But 
just after eight months, father’s illness forced Juma to leave the coveted job 
of marketing manager. However, even bigger and better challenges were awaiting 
him – he soon had to take up the reins of his father’s business.<br>
Facing the challenge<br>
“When I started, the biggest challenge was how to migrate from a one-man show to 
a big corporate set-up. But my brother (who is the chairman of this company) and 
I, decided to make it simple: When we go back home in the evening, we should not 
be bogged down by work pressure. The only question we had in mind was what we’ll 
do by earning so much money. You might get all the luxurious things in this 
world, but what lies beyond that? Keeping these simple facts in mind, we started 
taking some serious steps within the company,” the young managing director 
explains.<br>
With a well-chalked out plan, Juma started diversifying his business. He zeroed 
in on two vital sectors: franchise and real estate. “The main idea behind 
starting a franchise business is mainly to cash in on the boom in economy. I 
started this business in a smaller way because of apprehension about the success 
of a recycling product in Oman. I wanted to bring a product that is unique to 
the market, and should not have much problem even if there is competition. I can 
proudly say this strategy has worked, and after two years of operations, we have 
expanded our Cartridge World business to three outlets in Muscat,” explains Juma.<br>
The big thing, particularly in 2007, was that the company started getting big 
orders from corporates and some big business houses. What worked in its favour 
was that the company convinced its customers about the importance of going in 
for recycling cartridges, instead of using regular cartridges that were not so 
environment friendly. This created a large number of loyal customers for the 
company.<br>
The second part of his business was ERA real estate, a realty franchise for an 
American company. “The reason for entering into this segment was again to gain 
from the economic boom. Absence of any professional company in this sector 
helped our cause,” he says, adding, “ERA is the only real estate company that 
has its presence all over the Gulf. We thought we can extend our business to the 
entire region through this franchise.”<br>
Investing in people<br>
What Juma also achieved through this business was to explore Omani calibre. He 
has appointed many Omanis as agents of ERA and is even ready to make them 
partners in his business. By offering ‘very professional’ end-to-end services, 
Juma aims to make ERA the number one real estate company in Oman. <br>
“I want to achieve this through a true Omani professional calibre,” he says, 
adding, “investing in people is one of the hallmarks of our business. Any 
organisation that doesn’t invest in people will go down the drain. You can get 
the best out of your employees only if you invest in them. If you start looking 
at small, small aspects relating to employees, it gives you nothing in return.”<br>
Another art he has acquired over the time is to headhunt people. “I go to people 
and tell them that I have this project why don’t you join us. From lower rank to 
top officials I prefer to headhunt so that they understand what I am and what 
the reality about this organisation is. In this way, we can get the maximum work 
out of them,” avers Juma, who strongly believes in strong communication.<br>
So far, so good. But where does Juma see himself in five years? “I want to do 
more as a franchisee. I have two or three big ideas and some big ambitions. On 
an immediate basis, we are going to launch Caribou Coffee outlets in Oman. We 
have very grand plans for this franchise as this is one of the highly rated 
coffee shop chains in the world. I’m also going into tourism sector, where I’ll 
be launching a three star hotel in Muttrah by November. But then, I’m basically 
a highly unpredictable person. I’m the kind of person who wants to live for 
today. I’m not in a hurry. However, I want to create a set-up which makes my 
country and my people very proud.” <br>
He certainly looks to be on the right path. <br>
