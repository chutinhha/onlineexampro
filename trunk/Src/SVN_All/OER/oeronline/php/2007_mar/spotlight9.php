<p align="justify" dir="ltr"><font face="Verdana" size="3" color="#FF0000">
<br>
</font><b><font face="Verdana" size="4">Developing A New
Strategic Plan</font></b><font face="Verdana" size="1"><br>
<b>Gulf Air’s acting President and Chief Executive Ahmed 
Al Hammadi talks about the airline’s performance in
2006 and its Oman related operations in an interview 
with Akshay Bhatnagar</b><br>
<br>
<b>
<img border="0" src="spotlight16.jpg" width="175" height="188" align="left" hspace="5" vspace="5">How was the performance of Gulf Air in 2006?<br>
</b>Despite rapidly rising fuel prices, we are happy with our last year’s 
performance. Airline revenues for 2006 increased by between 5 and 6 per cent 
over the previous year. <br>
While Abu Dhabi traffic has reduced significantly, our capacity has remained 
substantially the same and the aircraft have been redeployed over our two hubs 
in Bahrain and Oman. The full impact of the two-hub model came into effect with 
the summer schedule in the second quarter of last year. The growth increased to 
41.3 per cent and 39 per cent in Bahrain and Oman, respectively.<br>
<br>
<b>What has been the progress on ‘Smart Airline, Successful Business’, the 
three-year strategic plan launched in 2006 by Gulf Air? <br>
</b>Gulf Air is currently in the process of putting forward a new strategic plan to 
cover the coming three years.<br>
<br>
<b>What have been the key achievements for Gulf Air in relation to its Oman 
operations in 2006? <br>
</b>We are an internationally recognised brand, ranked among the top 10 airlines in 
the world in product and service, and a large part of our efforts is to promote 
Bahrain and Oman to the rest of the world. With Oman being our other hub, we 
account for 55 per cent of the airport activity in Muscat, contributing millions 
of dollars to the Sultanate’s GDP. We are a major and growing contributor in 
Oman.<br>
<br>
<b>What has been the progress on establishment of an aircraft maintenance 
facility and training centre at Muscat? When do you think they will be ready?
<br>
</b>The Maintenance Repair Overhaul (MRO) operations have been transferred to SR 
Technics. We are in advanced discussions and in the process of finalising a 
long-term agreement, which once fully implemented will result in improving our 
service delivery.<br>
<br>
The Oman-based US$50 million three-bay hangar and maintenance facility in which 
Gulf Air will have a 10 per cent shareholding will provide maintenance 
solutions, based on customer requirements, to include aircraft and component 
services, technical training, logistics, on-wing and light engine services and 
engineering for Gulf Air and other third party customers. <br>
Using its pioneering Integrated Airline Solutions (IAS) product, SR Technics 
Switzerland provides full fleet technical management for Gulf Air’s fleet, which 
currently comprises 10 A320, six A330, nine A340 and nine B767-300 aircraft. <br>
<br>
While fleet management is undertaken from Switzerland, underpinned by a 
powerful, integrated IT system, maintenance and repair services are provided 
through the joint venture, local partnerships and SR Technics global component 
logistics network.<br>
<br>
However, on a broader scale, the cumulative benefits to the local economy are 
significant with suggested estimates of at least 1 per cent to the GDP of the 
Sultanate. When fully operational, the joint venture will create jobs for 850 
skilled and professional people, over a five-year period…<br>
<br>
<b>Last year, Gulf Air had stated to OER, “In terms of value, our focus is on 
premium segment, comprising of first class and business class segments, which 
contributes more than 80 per cent of our profits.” Do you plan to carry this 
forward in 2007? And how it is going to impact your business from the economy 
class?<br>
</b>Ultimately, the airline business is about the customer. Gulf Air has invested in 
and delivered world-class products and world-first initiatives directly aimed at 
serving our customers better, and putting Gulf Air in a tiny group of airlines 
that lead, not follow, in developing their products.<br>
<br>
We must ensure we not only meet but also exceed customer expectations. So far, 
we are doing this. Last year, we invested US$16 million in our premium seats and 
in-flight entertainment systems to create a definitive world-class product and 
the year before that we invested US$12 million.<br>
<br>
In first class, our new cabin concept has been designed to allow a private 
companion dining option with space for up to four diners, with 18 different meal 
choices on offer.<br>
<br>
The new seats offer unrivalled space, comfort, privacy and style. Finished with 
leather armrests, large head wings, lumbar support and individual power outlets 
for laptop use, the first class seats are state-of-the-art. On-board 
entertainment is delivered via an impressive 15-inch flat screen monitor and 
high-tech, noise-excluding headsets. Oversized tables create a comfortable space 
for work and dining.<br>
<br>
Passenger and industry recognition demonstrates that our business strategy, an 
important component of which is the continual improvement of products and 
services for the business traveller, is working. This innovation and development 
is set to continue.</font></p>
