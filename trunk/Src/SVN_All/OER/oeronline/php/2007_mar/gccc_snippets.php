GIC ‘06 profits to exceed US$600 mn<br>
The Gulf Investment Corporation (GIC) announced a 120 per cent year-on-year 
increase in net profits to US$600.1 million, up from US$272.8 million in 2005, 
the highest in its history.<br>
Consequently, return on average shareholders’ equity of approximately 42.9 per 
cent for the year went up by 16.4 per cent over the previous year, reaching 
US$1.9 billion. Total assets grew by 9 per cent to US$8.1 billion during 2006. 
With the calling of US$250 million of the unpaid capital, the GIC’s paid-up 
capital reached US$1 billion, a significant milestone, said GIC. The 
corporation, owned equally by six GCC states, declared a dividend of US$236 
million for 2006, approximately 31.5 per cent of its paid-up capital.<br>
Chairman of the Board, H.E. Dr Yousef Hamad Al Ebraheem, said, “This is the 
sixth consecutive year of profit growth subsequent to the strategic 
reorientation initiated in 2001, with net profits growing by several multiples 
during this period.” GIC has emerged as a powerful player in the region, winning 
major projects in Power, Utilities and other sectors, competing with 
international players at the highest level, Al Ebraheem added.<br>
“Recording a 155 per cent increase in gross operating income, the key 
contributor to net income continued to be ‘Projects’, a core business of the 
corporation,” said Hisham Al Razzuqi, Chief Executive Officer. “Strategic 
investments in the steel and chemicals sectors provided handsome returns, 
boosted by the strong economic growth in the GCC region,” he added.<br>
Healthy profit growth was also recorded within the portfolio of trading 
securities, the alternative assets portfolio, while recording enhanced fee-based 
income. Total investments and commitments in the GCC region at the end of 2006 
reached US$3.2 billion, 38 per cent of the total assets.<br>
Investment in projects is central to growth strategy, and assets within this 
business grew to US$1.07 billion, said GIC. The new investments during the year 
include participation in two major Power &amp; Utilities projects in Saudi Arabia, 
establishment of a holding company in Bahrain to consolidate interests within 
the Metals sector, investment in a major construction company in Qatar and 
several other projects within the GCC. <br>
GIC said it has a Tier I capital ratio of 20.9 per cent and a total capital 
ratio of 22.1 per cent as per Basel II guidelines. The corporation has an 
authorised capital of US$2.1 billion.<br>
Kuwait Finance House venture with Pacific Holdings<br>
The private equity division of Kuwait Finance House (Malaysia), Berhad, and 
Pacific Healthcare, an integrated healthcare provider with operations in 
Singapore, Hong Kong, India and China, have signed a joint venture agreement to 
invest up to S$32 million either in cash, or through the injection of 
synergistic businesses to establish new medical centres in the region.<br>
KFHMB and PHH have set up Aliph Pacific Pte Ltd, which will serve as the 
investment vehicle for the expansion of healthcare businesses in Asia. The joint 
venture will establish or model these healthcare facilities in line with PHH’s 
successful Singapore operations–with provision of integrated and 
multi-disciplinary specialist healthcare, including dermatology, dentistry, 
women’s healthcare and diagnostic imaging. Hospital management and consultancy 
are other areas the company will cover, the companies said.<br>
Several healthcare projects have been earmarked for the joint venture, the 
companies said. KFHMB has targeted healthcare as one of its core investment 
sectors and has identified Pacific Healthcare as an ideal strategic partner, 
said Mr K Salman Younis, Managing Director of Kuwait Finance House (Malaysia), 
Berhad.<br>
“Kuwait Finance House has the expertise in Islamic banking and finance as well 
as experience of healthcare ventures in Kuwait and India. We are therefore able 
to complement the technical know-how and premier quality healthcare services 
that Pacific Healthcare provides to consumers. The collaboration is indeed in 
line with our commitment to share our experiences in the Middle East with 
various industries in this part of the world,” he added.<br>
Dr William Chong, CEO of Pacific Healthcare said: “We believe that our 
partnership with Kuwait Finance House will allow us to cement our position as a 
premier healthcare provider in the region and further expand our brand 
internationally. Aliph is the first letter in the Arabic alphabet and the choice 
of the name for the company, Aliph Pacific Pte Ltd., reflects our intention to 
be at the forefront of healthcare.”<br>
