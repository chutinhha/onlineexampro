Trade rises in Dubai’s free zones<br>
Dubai’s eight free zones recorded an 8.9 per cent increase in non-oil foreign 
trade during 2006 as compared to 2005, with the total value reaching AED193.6 
billion against AED177.8 billion in 2005, according to Dubai World. “Imports 
through the free zones increased by 12 per cent, from AED99 billion in 2005 to 
AED111 billion in 2006, whereas exports grew by only five per cent – from 
AED78.7 billion in 2005 to AED82.6 billion in 2006,” Adel Al Ashram, Senior 
Manager, Statistics Department, Dubai World, said. <br>
China topped the list of exporters to the free zones with goods worth AED18 
billion, followed by Japan with AED10.7 billion, the US (AED8 billion), Finland 
(AED6.8 billion) and Germany with AED6.4 billion. Iran, the top destination for 
goods from Dubai’s free zones, received goods worth AED9.4 billion, followed by 
Saudi Arabia (AED7.2 billion), Iraq (AED4.7 billion), India (AED4.5 billion) and 
Belgium (AED4.3 billion). The Jebel Ali Free Zone had the lion’s share of total 
non-oil foreign trade volume accounting for AED136 billion – AED79 billion in 
imports and AED57.6 billion in exports. The Dubai International Airport free 
zone was second with AED46.8 billion – AED25.8 billion in imports and AED21 
billion in exports. The Dubai Metals and Commodities Centre (DMCC) registered 
AED6.3 billion – AED3.6 billion imports and AED2.7 billion exports – followed by 
Dubai Cars and Automotive Zone (DUCAMZ) with AED2.9 billion – AED1.8 billion 
imports and AED1.1 billion exports.<br>
While Dubai Internet City recorded AED534 million with AED472 million imports 
and AED62 million exports, the Dubai Media City accounted for AED251 million in 
business. The Dubai International Financial Centre (DIFC) and Dubai Health City 
recorded imports worth AED23.5 million and AED21.6 million, respectively.<br>
String of IPOs likely<br>
Some large institutions, including Emirates Post, Showtime Arabia, Dubai Bank, 
DP World, etc, are said to be planning IPOs during the current year, according 
to a report. The Sharjah-based Air Arabia recently announced its plans for an 
IPO in order to finance its new fleet acquisition.<br>
Relatively undervalued market multiples, sustainability of double digit core 
operational earnings growth, stable and strong macroeconomic fundamentals, 
relative positive differential in earnings yield to the interest rate and a 
holistic bottom-up approach for investments across the markets, may help 
engineer a much-wanted recovery in the UAE stock market, the report said.<br>
The current market PE ratio of 13x is more realistic when compared to near 40x 
and 30x PEs witnessed during 2005 and 2006, respectively, which makes a 
compelling reason to take an active long-term exposure to the market during the 
current year, according to Dubai-based Daman Investments research report.<br>
Unsustainable high valuations, large liquidity drains, margin call pressure, 
limited foreign investor participation, increasing interest rates and an 
increasingly negative regional geopolitical sentiment had brought UAE’s National 
Bank of Abu Dhabi (NBAD) Index down by 40 per cent during 2006 after the 
euphoric increase of 88 per cent and 101 per cent for 2004 and 2005, 
respectively.<br>
While a one-way rally may not be possible during 2007, a healthy return to 
equilibrium is the most desired outcome for the year, predicted Daman which 
launched its Daman Second Emirates Fund, an open-ended AED200-million fund which 
will focus on the UAE equities markets.<br>
The new fund’s minimum subscription amount was set at AED200,000. “We 
specifically wanted to structure a fund that would offer a very attractive 
opportunity to invest in UAE equities to a much broader audience,” said Daman 
Managing Director Shehab Gargash.<br>
The fund has already been seeded and has achieved a 6 per cent absolute growth 
during a period of approximately three months – over the same period when the 
NBAD index was down over 10 per cent.<br>
“This initial performance is a testament to our strategy which focuses on 
staying dynamic and creating an edge even in these unusual market conditions,” 
said Mr Gargash. He added that the investment threshold is significantly lower 
than the AED3 million minimum subscription required for Daman’s first 
UAE-focused fund – Daman UAE Value Fund. The previous fund provided investors 
273.84 per cent returns during its five-year tenure.<br>
Boosting business aviation<br>
Civil aviation authorities in the GCC region need to co-operate more to free up 
the region’s growing business aviation sector, according to His Highness Sheikh 
Ahmed bin Saeed Al Maktoum, President, Dubai Department of Civil Aviation, and 
Chairman of the Emirates Group.<br>
“For the sector to further develop, particularly within the GCC, there needs to 
be greater co-operation among civil aviation authorities towards open skies 
policies, which will free up the market, increase competition, generate new 
opportunities and deliver greater economic success,” His Highness Sheikh Ahmed 
told the Middle East Business Aviation (MEBA) 2007 conference.<br>
The business aviation fleet currently stands at 300 as against 250 last year – 
and the sector is expected to double over the next five years, to account for 40 
per cent of the total Middle East aviation market, said Sheikh Ahmed. The 
current chartered flights market is estimated at approximately US$400 million, 
and would exceed US$700 million if private jets are added.<br>
Her Excellency Sheikha Lubna Al Qasimi, the UAE Minister of Economy, said: “Just 
as in a number of other industries, it’s the regulatory side of the equation 
that has not been able to keep pace with the technological innovation, which is 
why countries are still negotiating air service agreements, why landing rights 
are still an issue hindering industry growth, and why low-cost airlines are not 
yet serving every market.”<br>
According to the Middle East Business Aviation Association (MEBAA), business 
aviation activities are growing at a rate of approximately 11 per cent a year. 
MEBA 2007 was organised by Fairs &amp; Exhibitions (F&amp;E), the name behind the Dubai 
Airshow on behalf of MEBAA, and featured 90 exhibitors from 20 countries.<br>
Sales at the show totalled US$957 million, with Airbus generating US$580 million 
worth of business. The UAE’s Wallan Aviation received a US$75 million order for 
Cessna aircraft, a US$250 million deal for 20 Hawker 750 aircraft from Raytheon 
Aircraft Company and a US $52 million Bombardier confirmation for four Learjet 
60XR from Saudi-based businessman Ghassan Tabbah Shalabi as well as buyers from 
Kuwait and the UAE.<br>
Moreover, Emirates-CAE Flight Training (ECFT) signed an agreement for the 
Gulfstream 550 full-flight simulator and associated training equipment for the 
Business Jet Training Programme. The 31-member MEBAA estimates that the region’s 
current business aviation fleet generates revenues in excess of US$500 million 
annually across various industry segments such as aircraft charter, maintenance, 
Fixed Base Operations (FBO) and refurbishment.<br>
– Michael Clarence (Dubai)<br>
Rights of consumers and firms strengthened<br>
In February, authorities in Bahrain made two extraordinary decisions, in turn 
serving the needs of consumers and companies. In short, one law requires firms 
to post prices of products in the most visible way. The law applies to all 
retail outlets including small shops selling fruits and vegetables.<br>
The Ministry of Industry &amp; Commerce, which issued the ruling, argued that the 
law was made partly in response to growing inflationary pressures. It is 
believed that the order would help pacing a cap on prices by discouraging firms 
from setting prices above market rates. This is particularly the case with 
respect to products sold under government-guided prices such as fruits and 
vegetables. Consumers at large welcomed the ruling. Organistions supporting 
consumer rights viewed the order as a victory for consumer rights. Yet, the 
biggest challenge relates to implementing the new order. Certainly, it is one 
thing to issue a rule but totally another to implement it.<br>
Concurrently, the Ministry of Industry and Commerce issued another ruling that 
proved popular with the business community. The order grants businesses the 
right to engage in sales or offering discounts on products without seeking prior 
knowledge of governmental departments or Bahrain Chamber of Commerce &amp; Industry 
(BCCI). It seemed that the ministry issued the new rule if only to appease the 
business community after being asked to post prices in the other order. It is 
believed that the new rule helps the cause of competition amongst firms, 
eventually benefiting consumers.<br>
Meanwhile, commentators urged the Ministry of Industry and Commerce and BCCI to 
consider developing laws dealing with return policy. The idea deals with 
granting buyers the right to return products within a certain period. At the 
moment, some retail outlets of international chains have their own independent 
policies regarding returns and exchanges. However, the demand stipulates that 
there should be a legislation handling the matter per se.<br>
Bahrain too casts doubts over GCC monetary union<br>
Bahrain has cast some doubts on the ability of Gulf Cooperation Council (GCC) 
countries in implementing the planned monetary union in 2010. In an extended 
interview with The McKinsey Quarterly published in January, Bahrain’s crown 
prince Sheikh Salman bin Hamad al Khalifa argued that technical matters could 
delay launching of the single currency. To be sure, Bahrain’s crown prince was 
in no way questioning the political will of GCC leaders. However, he questioned 
about reaching consensus on convergence criteria, namely inflation, public debt 
and interest rates.<br>
Under the planned monetary union project and the ensuing launching of a single 
currency, member states are required to meet five criteria dealing with 
inflation, short-term interest rates, budget balance, and public debt plus 
foreign exchange reserves.<br>
With respect to inflation, the rate should not exceed the weighted average of 
the member countries plus 2 percentage points. Additionally, short-term interest 
rate should not be more than the average of the lowest three countries plus 2 
percentage points.<br>
However, budget deficit could not exceed 3 per cent of the gross domestic 
product (GDP) and yet public debt stock should not exceed 60 per cent of GDP. 
Lastly, foreign exchange reserves should cover at least 4 months of imports. 
Bahrain has difficulty meeting the reserves requirement. In 2005, the value of 
four months imports amounted to US$2.6 billion. Bahrain’s reserves amount to 
nearly $2.2 billion<br>
Thus far only Oman had opted not to join the planned monetary union citing 
economic reasons. Omani authorities have argued that the sultanate could not 
meet the criteria for fear of undermining economic development efforts. As a 
developing country, Oman feels the need to be free to invest in the economy 
(partly to create jobs for locals) rather being restricted on debt and budget 
deficit levels. It is believed that Bahrain shares Omani concerns. However, 
unlike Bahrain, Oman decided to announce its position clearly and firmly.<br>
– Dr Jasim Husain Ali (Bahrain)<br>
