Qatar-Exxon gas project dropped<br>
Qatar Petroleum (QP) and ExxonMobil have dropped their plan to build a 
gas-to-liquids plant in Qatar due to cost escalation. Instead, they will focus 
their attention to developing part of the country’s huge North gas field. <br>
Qatar’s Energy Minister Abdullah Al Attiyah said this did not mean that other 
projects in Qatar were under any threat. In fact, very soon ground would be 
broken for a multi-billion-dollar GTL plant with Royal Dutch Shell. “GTL 
technology is expensive and very technical. Technology for the other projects is 
proven ... No other projects are under threat,” Al Attiyah said.<br>
Costs for the Exxon/QP GTL facility, which was to process gas into market-ready 
refined products, are said to have risen to as much as $18 billion from a 2003 
estimate of around $5 billion. The Exxon/QP GTL scheme, signed in 2004, had an 
initial budget of $7 billion. The plant was to have churned out 154,000 barrels 
per day.<br>
GTL plants process gas into clean oil products like low sulphur diesel, demand 
for which is growing on the back of tougher limits on emissions. QP has offered 
Exxon a role in the development of the Barzan gasfield, part of Qatar’s North 
field — the largest reservoir of non-associated gas in the world. QP also 
offered Exxon rights to participate in any future development at Barzan.<br>
Exxon’s Qatar country manager Alex Dodds said the Barzan cost would be similar 
to the company’s Al Khaleej gas project, also at the North Field. “We are 
pleased to have been the only international oil company selected to participate 
in the Barzan Project and look forward to continuing our successful partnership 
with Qatar Petroleum,” said Stuart McGill, senior vice-president of Exxon. <br>
A lot of gas projects in Qatar have inflated labour and raw material costs, 
aggravated by rising costs globally across an oil and gas industry, straining to 
bring new capacity online to meet rapidly rising demand for energy. ExxonMobil, 
the biggest foreign investor in Qatar’s energy sector, also has stakes in 
Qatar’s huge Rasgas and Qatargas liquefied natural gas projects. Qatar is home 
to the world’s third largest gas reserves after Russia and Iran.<br>
Qatar’s 2006 inflation at all-time high<br>
Qatar’s overall inflation touched an all-time high of 11.83 per cent in 2006, 
with the consumer price index (CPI) jumping to 133.23 from 119.23 in 2005. 
Soaring rent was the main driver of inflation, which for the first time hit a 
double-digit figure. The rate of inflation in 2005 was 8.8 per cent. Inflation 
stood at 0.2 per cent in 2002, 2.3 per cent in 2003 and 6.8 per cent in 2004. 
Data released by the Planning Council recently revealed the index weighting 
against the category that comprises rent rose 26 per cent in 2006. It stood at 
221.94 in December last year against 198.27 in 2005. <br>
The prices of garments and footwear rose considerably in 2006 as is evident from 
the sectoral index for the category. The index shot up to 114.46 in 2006 from 
101.81 the previous year. Another major jump was seen in the index relating to 
miscellaneous goods and services, in which personal luxury items and postal care 
services are included. Their index went up 13.6 per cent to 130.51 last year. 
The index weighting against the category of furniture, textiles and home 
appliances rose 3.9 per cent to 110.87 last year from 106.45 in 2005. The 
transport and communication expenses also climbed last year, as reflected in the 
index weighting against them. The index stood at 101.56 last year, up 1.9 per 
cent from 99.71 in 2005. The index on medical care and medical services stood at 
104.55 last year, up 1.2 per cent on 2005. The prices of most essential items 
have gone up in Qatar in the last several months primarily because of the impact 
of a rent spike on commercial and trading property. This has also led to a rise 
in the cost of services.<br>
