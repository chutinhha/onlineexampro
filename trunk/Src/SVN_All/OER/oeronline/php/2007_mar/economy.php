			<p align="justify"><font face="Verdana" size="1">
			As oil prices collapsed in the 1980s, many 
Gulf states began to cast a worrying eye over their demographic figures. With 
most families having three or more children at that time, governments in the 
Gulf Cooperation Council (GCC) zone began to realise that by the late 1990s, the 
region was going to suffer from an acute unemployment problem if prompt action 
was not taken. As oil money was not coming in as well as before, one of the 
concepts hit upon by GCC states was to begin increasing localisation of the 
workforce.<br>
<br>
However, now that the price of oil is relatively high and the need for economic 
growth is beginning to take precedence over other concerns, it will appear that 
the call for workforce localisation is beginning to peter out. One of the more 
strident supporters of workforce localisation, Saudi Arabia in January said it 
will reduce level of Saudiisation in key industries to encourage their growth. 
It is only the latest state in the region to announce a weakening of workforce 
localisation goals, and it certainly will not be the last in this regard.</font></td>
