<p align="justify">
<font face="Verdana" size="1">
Workforce localisation, whether Kuwaitisation in Kuwait, Emiratisation in UAE, 
Bahrainisation in Bahrain or Omanisation in Oman, was a goal born out of good 
intentions. Its creators saw that the economies of the GCC states were slowly 
falling out of the hands of local nationals, who found it hard to either get 
into the job market, or simply did not have the skills to shine in private 
industry. Although the states themselves had absorbed large amounts of 
unemployed nationals in the civil service and military, such jobs did not 
necessarily add to the wealth or skills set of the country. In many cases, the 
absorption of large amounts of nationals into such institutions was often done 
in lieu of an adequate welfare safety net. Equally, many local nationals found 
that the needs of private industry as well as its risks were far outweighed by 
the lower paid job security offered by the state.<br>
<br>
<b>Skills gap</b><br>
A problem is now growing within the GCC region in terms of workforce 
localisation. The original need for large numbers of both skilled and 
semi-skilled overseas workers has arisen again because of the explosive economic 
growth the region is seeing. Equally, another less well-known causative factor 
is beginning to rear its head: industrialisation policy. As states in the region 
have sought to diversify their economies away from an unbridled dependence on 
oil and gas and improve the downstream capabilities of industry, a new need for 
large numbers of unskilled and technically trained workers has come about. 
Often, much of the downstream attempts at industrialisation are seeking to 
compete head to head with other low-cost wage centres such as China. The 
plastics industry is a good example of this. Of course, the only way to secure 
such a large low-cost workforce if it is not present locally is to import it.<br>
<br>
Another sector placed under considerable strain by the recent boom in economic 
growth is that of construction. The explosion of high-profile construction 
projects in the Gulf, often called the “Dubai effect”, has seen the need for 
imported foreign labour grow at a soaring rate. For some states, such as the UAE, 
the goal of increasing its population through the importation of foreign 
nationals can be considered almost state policy.<br>
<br>
Also, a level of regional rivalry for key middle and senior management 
construction professionals, especially from states like Qatar, has seen wage 
rates for such people rise by some 25 per cent in the past year. Naturally, such 
wage rises also have to be seen in the light of the growing threat of imported 
inflation coming through given the continued US dollar peg of GCC currencies, 
with the UAE and Qatar being the focus of much of this current round of 
inflationary activity. Finally, another key problem that many employers are 
facing is in finding trained local employees for more senior positions. This is 
well demonstrated in the problem that petrochemical companies face in Saudi 
Arabia as they try and meet the Saudiisation standard.<br>
<br>
Saudi Arabia has only one higher education facility, the King Fahd University of 
Petroleum and Minerals in Dhahran that is currently producing educated 
professionals, though another programme has recently begun to increase their 
numbers. Of the students, a full 50 per cent are normally taken in by Saudi 
Aramco, while other companies looking to fulfil their Saudiisation quotas have 
to fight over the rest. This has led to wage inflation, as local workers freely 
move between the better offers given by competing employers. As they are in 
short supply, companies find it hard to meet their minimum workforce 
localisation quotas and are forced to scale back production because they are not 
allowed to bring in more foreign workers to solve their staffing issues.<br>
<br>
<b>Free zones</b><br>
Cracks in the workforce localisation campaign in the GCC region have long been 
apparent. Free economic zones (FEZs) are often created to allow for full foreign 
ownership and no restrictions on the importation of foreign labour, as happens 
in the UAE. Other industries, especially those like textiles that are dependent 
on low-cost labour, have managed to have their quotas either reduced or 
eliminated. In seeking a balance between workforce localisation and foreign 
labour dependent economic development, the latter is increasingly being chosen.<br>
<br>
Oman has long been one of the more active states in ensuring workforce 
localisation, and has sought to back up this campaign through the provision of 
more educational facilities to produce the workers it needs. Although around 
one-quarter of Oman’s population is made up of foreign nationals, this compares 
favourably with other states in the region such as Saudi Arabia (around 30 per 
cent) and the UAE (up to 80 per cent). Even in Bahrain, which in theory has a 
reasonably low-cost labour pool and a substantial unemployment problem, the 
proportion of foreign workers in the general population is over one-third.<br>
<br>
However, even in Oman, a crack in the Omanisation policy has opened up: the 10 
per cent minimum set down for free zone areas. While some may characterise this 
as a step down from the previously vigorous government policy of promoting 
Omanisation through the imposition of strict quotas and profession restrictions, 
it is better seen in the light of the present regional trend away from workforce 
localisation. The trend is quite strong. The only way to encourage economic 
diversity and bring in more foreign investors is to accede to their needs for 
low-cost, easily available labour.<br>
<br>
But the question is slowly arising: Is the creation of low-cost labour dependent 
industry the solution to creating economic diversity? By creating large amounts 
of such industry, it is assumed that a proportion of locals will receive jobs in 
the process as a form of economic spin off. The chances increase as the economy 
grows overall, especially in the services sector. While trade statistics look 
good for now, will such industry play a long-term role in economic development 
or once again delay the growing problem of local unemployment? That, as they 
say, is something we are presently in the process of seeing.<br>
