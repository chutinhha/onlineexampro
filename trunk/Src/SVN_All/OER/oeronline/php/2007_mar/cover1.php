<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Even though retail constituted more than 50 per cent of the bank’s operating 
income, but it was corporate banking that grew at a faster rate. In 2006, 
BankMuscat’s operating income from corporate banking grew by 39.2 per cent, 
compared to 24.8 per cent growth in retail banking. <br>
<br>
<b>Meeting Wider Needs<br>
</b>National Bank of Oman was also on a roll. The tough Aussie Andrew Duff, NBO’s 
CEO, again did the trick. After placing the bank back on track in 2005, he 
consolidated the gains in 2006. The overall buoyant economic conditions also 
were in his favour. <br>
<br>
The bank recorded a net profit of RO30.4 million, surpassing RO20.3 million 
reported for 2005 by 50 per cent. The growth in total assets during the year saw 
the bank crossing the RO1-billion-mark for the first time in its 32-year 
history. This was linked to the growth in deposits by RO200 million (32 per 
cent) and growth in net advances by RO162 million (30 per cent). These increases 
reflect a growth trend and are consistent with NBO’s strategy to grow quality 
loans and deposits.<br>
<br>
<img border="0" src="cover6.jpg" width="232" height="308" align="right" hspace="6" vspace="4">Talking about the bank’s performance, NBO’s Chairman Sheikh Suhail Bahwan 
stated, “As the second-largest bank in the country, we are well-positioned to 
cater to the changing needs of the market. Also, while the bank will have to 
continue to grow its net interest income, the reliance on such income will 
reduce and the share of fee-based income on a recurring basis as a percentage of 
the total income will increase.” <br>
<br>
A closer scrutiny of NBO’s financial statement for 2006 reveals that consumer 
banking contributed a great deal to the bank’s performance. The net profit from 
consumer banking went up by a whopping 94 per cent, with RO18.4 million in 2006 
compared to RO9.5 million in 2005. At the same time, the net profit from 
corporate banking showed a marginal growth of 0.3 per cent only.<br>
<br>
Though Oman International Bank had a decent run in 2006, but on parameters such 
as interest income, customer deposits, loans and advances and net profits, most 
other banks outscored OIB. For example, OIB increased its customers’ deposits by 
just 3.94 per cent to move from RO659 million in 2005 to RO685 million in 2006. 
On the other hand, NBO increased its customers’ deposits by 32.46 per cent in 
the same period overtaking OIB to occupy the second slot in the category. On the 
loans and advances front, OIB registered a growth of just 0.97 per cent whereas 
most other major banks clocked double digit growths. <br>
<br>
<b>Focused Marketing</b><br>
<img border="0" src="cover7.jpg" width="226" height="308" align="left" hspace="6" vspace="4">For Bank Dhofar, the year 2006 was a blessed year as some of the financial 
indicators of the bank recorded good growth. The total assets of bank increased 
to RO694.79 million as compared to RO618.22 million in 2005, representing a 
growth of 12.39 per cent. The bank achieved a net profit of RO20.13 million in 
2006 compared with RO14.20 million in 2005, a growth of very high 41.77 per 
cent. <br>
<br>
Ahmed Al Shanfari, CEO Bank Dhofar said, “The banking sector in Oman has 
received an impetus from the Sultanate’s consistent economic growth, increased 
allocation of funds by the Government for development projects, launch of mega 
industrial and tourism projects by the private sector and increased savings. 
Apart from this, the buoyant capital market conditions and boom in the real 
estate sector have been key drivers behind the good performance of the banking 
sector. While Bank Dhofar too has benefited by this growth, its focused 
marketing strategy, competitive pricing and excellence in delivery of both its 
products and services, has helped it move towards a leadership position.”<br>
<br>
Bank Dhofar is looking at further improving its good performance in the coming 
years. During 2006, its Board of Directors appointed Booz Allen Hamilton as 
consultant to develop human resources, to study ways to enhance efficiency of 
various banking activities, to design a new organizational chart and to make a 
strategic plan to achieve the required growth.<br>
<br>
<img border="0" src="cover8.jpg" width="225" height="308" align="right">For Oman Arab Bank, net profit for 2006 was RO15.11 million. This was 10 per 
cent more than 2005’s net profit of RO13.69 million. The interest income 
increased by 23 per cent to RO29.3 million in 2006 from RO23.8 million in 2005, 
mainly due to a 21-per cent increase in gross loans and advance from RO288 
million in 2005 to RO350 million in 2006.<br>
<br>
Abdul Kader Askalan, CEO, Oman Arab Bank informed, “The customer deposits have 
been consistently growing from RO338 million as in 2005 to RO394 million as in 
2006. The inter-bank interest and income from Central Bank CDs are higher 
compared to the previous years as a result of increased liquidity and higher 
interest rates on CDs. Investments in CDs is at RO75 million… other operating 
income decreased by 9 per cent compared to 2005 mainly due to loss on 
investments as a result of the downtrend in the Muscat Securities Market.”<br>
