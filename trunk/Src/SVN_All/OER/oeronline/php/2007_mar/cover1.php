</font><font face="Verdana" size="4">
On A High…</font><font face="Verdana" size="1"><br>
<b>With Omani banks setting new benchmarks with record-breaking financial growth, 
Akshay Bhatnagar takes stock of the commercial banks’ performance in 2006 </b> <br>
<br>
The market leader BankMuscat set the pace for the stupendous performance of the 
banking sector in Oman in 2006. With net profits up by 33 per cent, net interest 
income increasing by 27.5 per cent, and net loans and advances portfolio growing 
by 33.7 per cent, it was smooth sailing for BankMuscat during the year. In terms 
of credit risk management, the bank made some interesting moves that pointed at 
the sweeping changes taking place in the economy. <br>
<br>
Compared to 2005, BankMuscat increased its exposure to sectors such as mining &amp; 
quarrying, real estate, services and transport during 2006. Its gross loans and 
advances increased from RO23 million in 2005 to RO65 million in 2006 in mining &amp; 
quarrying, up by a whopping 180 per cent. Similarly for real estate sector, in 
which the bank’s lending increased from RO26 million in 2005 to RO56.7 million 
in 2006, almost a 117-per cent raise. In services sector, lending increased to 
RO163.9 million in 2006 from RO107.7 million in 2005, up by 52 per cent. <br>
<br>
With a greater appetite for money in the market, BankMuscat’s offtake of 
personal loans increased by 36.5 per cent. The personal loans’ share in the 
bank’s gross loan and advancement also increased marginally. The bank gave 
RO832.3 million as personal loans in 2006 compared to RO609.6 million in 2005.
</font></p>
