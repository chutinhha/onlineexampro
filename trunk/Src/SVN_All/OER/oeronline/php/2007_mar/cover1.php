      <p align="justify"><font face="Verdana" size="1">
      <b>New kids on the block</b><br>
      <br>
The banking industry in Oman, growing at a scorching pace, is attracting fresh 
investments from foreign and local players trying to gain a foothold in the 
Sultanate market. At least three new commercial banks are expected to join the 
ranks in 2007.<br>
      <br>
The Bank of Beirut – one of the top 10 Lebanese banks – has just launched its 
overseas banking operations in Oman in February. This is the bank’s first 
substantial presence in the Middle East and the Gulf region. Ramy F Zambarakji, 
MD of the bank said, “We’re investing RO10.5 million initially as capital 
investment. Oman’s a growing economy and there are tremendous opportunities 
here. Oman’s foreign banking regulations are very favourable at par with many 
GCC countries, and in some respects are even better.”<br>
      <br>
In January, Qatar National Bank (QNB) announced that it has received the 
approval from the Central Bank of Oman to open a full services branch in Muscat. 
The new branch will offer comprehensive services in the Omani market. QNB 
indicated that it would increase the trade volume between the two countries, 
facilitate the banking transactions and elevate the customer service standards. 
QNB has the highest credit rating among Qatari banks from leading rating 
agencies, including Standard &amp; Poor’s, Moody’s, Fitch, and Capital Intelligence.<br>
Bank Sohar, which is expected to start operations from April, closed a RO20 
million-worth initial public offering recently. More than 33,000 investors 
received share allotments. The bank offered 40 million shares at 520 baisas per 
share, which included an issue expense of 20 baisas. The IPO was oversubscribed 
by more than five times to raise RO123 million. To start with, the bank will 
have a paid-up capital of RO50 million in which RO30 million are contributed by 
promoters and RO20 million by investors. This will be raised to RO 100 million 
within three years when promoters and investors bring in remaining 50 per cent 
of the nominal value.</font></td>
