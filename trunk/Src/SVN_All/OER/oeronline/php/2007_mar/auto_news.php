</font><b><font face="Verdana" size="2" color="#CC3300">A smart offer<br>
</font></b><font face="Verdana" size="1"><br>
<img border="0" src="auto1.jpg" width="250" height="103" align="left" hspace="6" vspace="4">Suhail 
Bahwan Automobiles has launched a ‘Smart Drive’ Offer during which customers who 
buy Nissan passenger cars and 4WD station wagons for personal use will get free 
registration and free one-year comprehensive insurance (on 2005 and 2006 
models). Free insurance is also available on the 2007 models of Nissan TIIDA, 
Altima and Maxima. Cars bought for use as taxis, or for a driving school, will 
be entitled to free insurance only up to the limit of the basic premium and any 
additional premium will have to be paid up by the customer. Customers can also 
avail a Comprehensive Vehicle Maintenance Plan for 2 years/ 30,000 km (whichever 
is earlier). Diesel vehicles like the Nissan Pickup, Urvan, Civilian or Patrol 
(2005 and 2006 models) will be offered 2,000 litres of free diesel. Nissan 
vehicles purchased as taxis and driving schools cars will be painted in the 
approved colour scheme and also fitted with light boxes. Suhail Bahwan has also 
introduced specific finance programmes with special offers for teachers.<br>
