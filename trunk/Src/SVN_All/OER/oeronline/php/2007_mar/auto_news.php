<b><font face="Verdana" size="2" color="#CC3300">Altea XL bags design award<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="auto2.jpg" align="left" hspace="6" vspace="4">SEAT’s 
Altea XL won its first award in Poland where panel members of ‘Poland’sCar No. 
1” presented it with the special prize in the design category. The ‘Poland’s Car 
No. 1’ is part of the ‘Auto 1 of Europe’ competition, which is organised in 25 
countries. The chief editors of the award’s participating media evaluate the 
competing vehicles from each country on a track. The SEAT Altea XL was created 
in response to those who admire the Altea’s sporty, dynamic silhouette, but need 
more loading space for the whole family. With 18.7 centimetres bigger than the 
Altea, the XL features a boot with 532 litres of capacity and numerous 
possibilities of rearranging the interior, all the while maintaining the new 
generation of SEAT models’ sporty looks and strengthening its family approach 
with elements such as the luggage racks. The new Altea XL is expected to be in 
Oman in May-June 2007.<br>
