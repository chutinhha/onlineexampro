<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="4">Accelerating Growth</font><font face="Verdana" size="1"><br>
<br>
<b>A striking aspect of the 2007 budget is the growth in non-oil revenues, most of 
it coming from taxes, says Majid Al Toky</b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="coverstory5.jpg" width="400" height="266"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">The Sultanate’s 2007 general state budget, unveiled earlier this month and 
ratified by Royal Decree in January, has been met with a positive response 
across industry sectors. In an economy where oil and gas are so critical, the 
sector is invariably at the heart of the annual budget, and the 2007 budget is 
no exception.<br>
<br>
The year 2006 exceeded budget expectations, both for oil and gas and the economy 
as a whole. In fact, performance in the oil and gas sector may be seen as 
guiding the economy generally. The 2007 budget forecasts revenue growth of 25 
per cent on the previous year, which runs alongside the 25 per cent increase in 
oil price budgeted for 2007. Despite the 2006 budget anticipating a general 
deficit of RO650 million (6 per cent of GDP), the government expects 2006 
figures to reveal an actual surplus of revenue over expenditure of RO2,400 
million, around 18 per cent of GDP.<br>
<br>
The 2007 budget predicts a deficit of RO400 million, around 40 per cent lower 
than the previous year, and experts see the government’s anticipated budgetary 
deficit for 2007 being easily made up, with a significant revenue surplus 
likely, as in the previous year. GDP is expected to grow by 6 per cent over the 
course of 2007, and the climate of high oil prices has clearly fuelled the more 
rapid growth in Oman’s economy.<br>
<br>
However, the fact that the Omani economy continues to defy expectations is due 
not only to the price of oil, but also to the government’s prudent fiscal 
policies. For 2006, oil prices were forecast at US$32 per barrel. Actual average 
prices for the year 2006 were a record-breaking US$62 per barrel. The government 
has continued this prudent approach in the 2007 budget, estimating oil prices at 
US$40 per barrel, against market expectations in excess of US$50 per barrel. 
Revenues are expected to increase by 25 per cent, while expenditure is increased 
by 15 per cent as compared to the 2006 budget. Although the outlook is 
optimistic for Oman’s economy, world oil prices fell in the latter part of 2006, 
and continue to fall due to a mild US winter and consequent reduced demand for 
petrol and heating oil. Earlier this year, oil prices saw their sharpest drop in 
two years (US$2 per barrel), and economists foresee a fall in oil prices over 
2007 as compared to 2006, when they peaked at US$78 per barrel (July 2 006 
figures). World oil demand is nevertheless expected to rise by 1.6 per cent to 
85.9 million barrels per day this year according to the International Energy 
Agency’s forecast on December 13, 2006, and Oman’s oil revenues for 2007 are 
expected to increase by approximately 20 per cent on the previous year’s budget 
to RO3,015 million.<br>
<br>
Notwithstanding, there are a number of factors which mitigate the Omani 
economy’s emphasis on oil for 2007, if only to a very limited extent. The 2007 
general state budget forecasts a proportionately greater increase in gas 
revenues as compared to oil revenues (anticipated gas revenues for 2007 are put 
at RO 550 million, an increase of 40 per cent on the previous year, while the 
increase is 20 per cent for oil.) Indeed, one of the most striking aspects of 
the 2007 budget is the growth in non-oil revenues. Apart from gas, revenues from 
taxes and fees are estimated at RO360 million, a 46 per cent increase from the 
2006 budget. Revenue from income taxes is expected to grow by 83 per cent from 
the RO72 million featured in the 2006 budget to the RO132 million budgeted for 
2007. With increased non-oil revenues and lower oil prices anticipated for 2007, 
oil revenue share in total Government revenues is expected to fall from 70 per 
cent (2006) to 67 per cent (2007).<br>
<br>
<img border="0" src="coverstory6.jpg" width="204" height="366" align="left" hspace="5" vspace="5">Small though this fall in oil revenue share as a proportion of overall revenue 
may be, the 2007 budget clearly reflects government emphasis on diversifying 
revenue sources, and improving the competitiveness of Oman’s economy. Mindful of 
Oman’s limited crude oil lifespan, the government is acutely aware of the need 
to diversify revenue sources in order to cater for the economy’s well-being in 
the long-term, and non-oil exports of Omani origin are expected to grow by 11.4 
per cent over the coming year. This does not detract from the fact that oil 
remains and will continue to be the most essential part of Oman’s economy and 
the single largest source of revenue for the next 20-25 years at least. Indeed, 
oil and gas production expenditure is increased under the 2007 budget by 32 per 
cent to RO1,205 million (up from the RO914 million budgeted for 2006), while 
daily average production for 2007 is estimated at 730,000 barrels per day, with 
exports expected to grow by 8.2 per cent.<br>
<br>
Outside the realm of industry, there are notable increases in public expenditure 
in social welfare–education, healthcare–which will no doubt be welcomed by all. 
Business leaders across the spectrum of sectors seem to have unanimously 
welcomed the 2007 budget. The oil and gas industries are no exception, and the 
increased expenditure, cautious pricing, and economic prudence which 
characterise this year’s budget can only bode well for the central pillars of 
Oman’s economy.<br>
<br>
The pragmatism and aptness of the 2007 budget for the oil and gas sectors was 
never really in question. It would be more pertinent to look at the budget’s 
implications for non-oil sources of revenue, and how this budget accounts for 
and pre-empts changes in Oman’s economy to ensure long-term viability.<br>
<br>
Increases in government revenue from liquid natural gas and performance in 
non-oil sectors generally have certainly left their mark on the current budget, 
and the increased estimates of non-oil revenues for the year ahead reflect the 
government’s ongoing pursuit of economic diversification. The trend in Oman is 
towards continued economic growth, and the budget is geared towards sustaining 
this growth in 2007. The true extent of this growth will depend heavily on the 
course of oil prices, which are expected to remain relatively stable, although 
geopolitical and meteorological conditions will need to demonstrate equal 
stability.<br>
