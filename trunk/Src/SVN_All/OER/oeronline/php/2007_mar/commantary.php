<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4">Just Gassing?</font><font face="Verdana" size="1"><br>
<b>Will Vladimir Putin ‘walk the talk’ on creating a Gas Cartel at the Doha 
conclave? Ramesh Kumar</b><br>
<br>
<img border="0" src="commentary1.jpg" width="150" height="204" align="left" hspace="6" vspace="4">Who says the erstwhile Union of Soviet Socialist Republic (USSR) has lost its 
bite with the death of cold war in late 1980s? Listen to Russian President 
Vladimir Putin’s utterances in the past few weeks as he jets across the Gulf 
region shaking hands with the likes of Emir of Qatar, Mahmoud Ahmadinejad of 
Iran and King Abdullah bin Abdul Aziz al-Saud of Saudi Arabia. Putin has been 
openly talking about forming an Organisation of Petroleum Exporting Countries 
(OPEC) type gas cartel – Gas PEC is the favourite term – whereby natural gas 
producing nations come together to “coordinate policies in the gas sphere to 
ensure uninterrupted supplies, but should not fix prices like OPEC.”<br>
<br>
‘If oil exporting nations can come together, why not gas exporting nations?’ is 
Putin’s refrain. Sounds logical. Russia, the world’s largest gas producer, is 
taking its role seriously. Qatar, one of the GCC members and owner of world’s 
third largest gas reserves (11.2 trillion cubic metres), is more than eager to 
join ranks with Putin. Qatar, incidentally, is also an OPEC member, whereas 
Russia is not.<br>
<br>
Algeria is a new convert to the concept of having a Gas PEC. Iran, developing 
its gas fields, is also egging Russia to go ahead with the gas cartel. Libya and 
the Central Asian countries, along with Mauritania, Mali and some Central 
African countries, could add up the muscle, given the gas reserve prospects in 
their respective countries. <br>
<br>
Energy analysts figure out that if Russia, Qatar, Algeria and Iran were to come 
together under the umbrella of Gas PEC, they alone will constitute 30 per cent 
of the world’s gas production. Their aggregate proven reserves exceed 60 per 
cent of the total which is comparable to OPEC’s respective share in the global 
oil reserves (68 per cent). <br>
<br>
<img border="0" src="commentary.jpg" width="226" height="464" align="right" hspace="6" vspace="4">With Russia at the forefront championing the Gas PEC proposal, it is not 
surprising that the United States and the European Union are rattled. “It’s not 
clear what a Gas PEC could set itself to do in terms of price targeting and 
volume management,” is how the US Energy Secretary Samuel Bodman reacted in 
mid-February – a fortnight after Russian President set the cat among the 
pigeons.<br>
<br>
The US would attempt everything to discourage any plans to form an overseas Gas 
PEC. Why? Open, not limited, markets are the key to meeting future energy 
demand, asserts Bodman.<br>
<br>
Though fingers are pointed at Iran for queering the pitch – in an attempt to 
showcase its influence in the current geopolitical scenario, where it is pitted 
against the superpower over nuclear issues – the originator of the Gas PEC idea 
is none other than Putin himself.<br>
<br>
Energy watchers point out that way back in 2002, Putin had offered to create a 
“Eurasian gas alliance” during his talks with Turkmen President Sapamurat 
Niyazov. Turkmenistan did not bite the bullet then. Will it now? <br>
<br>
But what really prompted the resurgence of this Gas PEC idea now? “In October 
2006, the appeals for setting up a Gas PEC became stronger. The main reason was 
discontent with the European Union’s energy policy. Its advocates see EU 
strategy as an attempt to set up an association of consumer countries, aimed at 
driving a wedge into the ranks of gas producers in line with the divide-and-rule 
principle. In other words, the goal is to let the consumers dictate prices to 
the producers,” writes Dr. Igor Tomberg, Senior Research Fellow at the Centre 
for Energy Studies of the Institute of World Economy and International Relations 
at the Russian Academy of Sciences.<br>
<br>
Russia’s January 2006 face-off with Ukraine over gas supply – along with the 
December decision to hike its gas price (from US$46.67 per thousand cubic metres 
to US$200) – has stirred the conscience of European Union, the biggest buyers of 
Russian gas.<br>
<br>
‘How much to depend on Russia as a reliable energy partner’, is the question now 
reverberating in the 
European capitals. That’s when the EU began
revisiting its old plans to repose faith in coal and nuclear energy. ‘What if 
Russia decided to sell its gas to China instead of Europe?’ A big if, indeed.<br>
<br>
“Despite the apparent differences between Russia and Europe, each side needs the 
other more than they care to admit,” point out Ivo Bozon, Warren Campel and 
Thomas Vahlenkamp in their analysis of the Europe-Russia energy equation in one 
of the recent issues of McKinsey Quarterly. <br>
<br>
The idea of Gas PEC may sound terrifying, but actually it is not, argues 
investment analyst Daniel Gross. “Cartels seek to influence prices by setting 
production quotas. But autocratic countries frequently find reasons to cheat, 
producing more when prices are high. A gas cartel could be just as fractious. 
The notion – that Vladimir Putin, Mahmound Ahmadinejad and Muyammar Qaddafi 
would reliably stick to the terms of a cartel pact – is laughable,” he adds. <br>
<br>
Significantly, the US, Canada, the Netherlands, Norway and the United Kingdom 
account for 35 per cent of total gas production. They have not ideated on 
creating a cartel so far. But the Putin gas missile may force them to reconsider 
such an eventuality, and may decide to pump in more investment to jack up 
domestic gas production – or they may hasten the pace to scout around for fossil 
fuel alternatives. After all, no one can dispute the fact that clean burning 
natural gas, rather than coal, is the fuel of the future.<br>
<br>
<img border="0" src="commentary2.jpg" width="300" height="169" align="left" hspace="6" vspace="4">Critical moments invariably bring about changes in human behaviour. A recent 
International Energy Agency (IEA) report suggests that the fear of a 
Russia-induced energy crisis has led to a 0.6 per cent fall in the collective 
consumption of oil by the 30 members of the Organisation of Economic 
Development. This is the 1st such behavioural change in 206 years! <br>
<br>
Sergei Blogov of ISN Security Watch dismisses the fear over Gas PEC. “Such a 
cartel would have the ability to drive gas prices up, even with a moderate 
supply limitation. However, such a cartel’s potentially highly diverse 
membership will inevitably entail difficulties in reaching consensus on output 
issues, notably on the divisible issue of gas output quotas,” he reasons. <br>
<br>
Agreeing with the Blogov line of argument, Gross adds, “The hyper-dynamic and 
constantly changing economic climate makes the enterprise of controlling 
commodity prices a fool’s errand. Over the past decade OPEC has done a poor job 
of keeping oil prices where it wants them to be.” According to him, OPEC did not 
want a US$10 barrel in 1999 any more than it wanted a US$70 barrel in 2006.<br>
<br>
Factors beyond the control of OPEC governments “conspired to move prices 
dramatically,” points out Gross – adding that the same factors that wreaked 
havoc with the pricing of oil are likely to affect the pricing of natural gas as 
well. <br>
<br>
The factors beyond OPEC control, which Gross is referring to, include the rise 
of India and China with a huge appetite for energy, the advent of giant hedge 
funds speculating in commodities, the spectre of terrorist attacks, and the 
possibility of a war in the Middle East – or even a recession in the United 
States. Oil is traded in a global marketplace, whereas natural gas is currently 
sold in fragmented markets mostly through long-term futures contracts. So far, 
gas-producing countries are acting like a consortium to influence the markets. A 
Gas PEC may change all this.<br>
<br>
But will Putin ‘walk the talk’? That question is
likely to get answered in March 5-6, when Russia and other natural gas producing 
nations meet in Doha to discuss the possible establishment of an alliance among 
major gas exporters. Oman, a 
non-OPEC member but an emerging natural gas player, will be equally keen to know 
the outcome 
