		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><font face="Verdana"><font size="4"><b>Riding High</b></font><font style="font-size: 14pt"><br>
		</font><b><font size="1">The Saudi Arabian economy is reflecting buoyancy in non-oil export of 
		goods and 
		growth in consumer confidence, reports Michael Clarence</font></b><font style="font-size: 14pt"><br>
		</font><font size="1">
		<br>
		Saudi Arabia’s non-oil exports are set to reach SR79 billion this year 
		against SR71 billion in 2006, according to a report by the Ministry of 
		Finance. Basing its forecast on major increase in non-oil exports to 
		Iraq and East Asia, as well as the access of its products to the 
		European markets following the entry into the World Trade Organisation (WTO), 
		the report noted that the value of these exports increased by 10.3 per 
		cent.<br>
		<br>
		The WTO entry has enabled Saudi Arabia to enhance its competitive 
		advantages, especially in the petrochemical industry, and strengthened 
		its position in international markets, said the report.<br>
		<br>
		Recently the Saudi Exports Development Centre, an affiliate to the 
		Council of the Saudi Chambers of Commerce and Industry, had predicted 
		non-oil exports would touch SR80 billion in 2007 and pointed out that a 
		number of Saudi projects which will begin operations during the year 
		will help raise the value of such exports.<br>
		<br>
		Besides petrochemicals, other sectors that are increasingly active in 
		the Saudi economy include construction materials, electrical goods, 
		machinery and industrial equipment, metal products, wood products, food 
		and beverages, besides textiles and garments. According to the Centre, 
		Saudi non-oil exports jumped from SR35.7 billion in 2003 to SR48 billion 
		in 2004, and SR60.5 billion in 2005.</font></font></p>
		<p align="center">
		<img border="0" src="gccdairy.jpg" width="460" height="301"></p>
		<p align="justify"><font size="1" face="Verdana">Quoting figures from the Central Department of Statistics and 
		Information, a Global Investment House (GIH) report said Saudi Arabia’s 
		total exports of goods and services are expected to grow by 15.1 per 
		cent, reaching US$215.5 billion in 2006. Non-oil exports of goods are 
		expected to grow by 10.8 per cent amounting to US$21 billion, 
		representing 10.1 per cent of total goods exported. The total imports of 
		goods and services are expected to grow by 27.2 per cent in 2006 
		amounting to US$104 billion, while goods imported are expected to grow 
		by 9.4 per cent reaching US$64.9 billion, said GIH.<br>
		<br>
		The trade balance is estimated to record a surplus of US$147.6 billion 
		in 2006, registering an increase of 17.5 per cent. The current account 
		balance increased to US$102.7 billion in 2006 as compared to US$90 
		billion the previous year, added the GIH report.<br>
		<br>
		GIH said that though the Saudi economy is diversifying and non-oil 
		sector is recording strong growth, the oil sector will continue to 
		dominate the exports. <br>
		<br>
		“However, in terms of sectors, the off-shoots of the oil sector such as 
		chemical products and plastic products have registered strong growth as 
		the new capacities came on-stream. Chemical products registered a yearly 
		growth of 30 per cent as their exports in 2005 amounted to US$6.5 
		billion. We expect allied sectors to show more than 25 per cent growth 
		in the next couple of years as new plants and capacity expansion get 
		completed by the major player,” said the GIH report.<br>
		<br>
		<b>Survey shows high confidence</b><br>
		With a current MasterIndex of 97.3 against 88.5 six months ago, Saudi 
		Arabia topped the Middle East &amp; Levant (MEL) region in the Consumer 
		Confidence survey for the second half of 2006. The consumers are 
		optimistic and confident on all five economic factors surveyed for 
		MasterIndex – Employment, Economy, Regular Income, Stock Market and 
		Quality of Life.<br>
		<br>
		Saudi Arabia rose from second place in the previous survey conducted six 
		months ago in which Kuwait was ranked top. The countries surveyed were 
		Egypt, Kuwait, Lebanon, Saudi Arabia, the UAE, South Africa and India.<br>
		<br>
		<img border="0" src="gccdairy1.jpg" width="300" height="213" align="left" hspace="6" vspace="4">The Kingdom saw the most significant increase in consumer sentiment on 
		the Stock Market, which rose to 95.8 from 82.1, up 13.7 points from the 
		previous survey. Perceptions on Quality of Life rose to 97.6 from 92.3, 
		Regular Income rose to 96.5 from 92.8, sentiment on Economy improved to 
		98.8 from 89.7 and Employment rose to 97.9 from 85.5, the second highest 
		leap.<br>
		<br>
		Commissioned by MasterCard Worldwide and conducted bi-annually in 
		selected markets in South Asia, Middle East and Africa (SAMEA), the 
		survey analyses consumers’ perceptions of economic conditions over the 
		next six months. The MasterIndex has a range of 0 to 100. A score above 
		50 indicates that consumers are optimistic about the economic climate, 
		while a score below 50 signals consumers are pessimistic.<br>
		<br>
		Saudi Arabia was followed by Kuwait where the result stayed the same, 
		(94.5 vs 94.5 six months ago). These were followed by South Africa (86.5 
		vs 91.1 six months ago), the UAE (80.0 vs 77.5 six months ago) and 
		Lebanon (67.6 vs 51.3 six months ago). For India the score dropped from 
		76.4 six months ago to 65.1.<br>
		<br>
		Dr Nasser H. Saidi, Chief Economist of the Dubai International Financial 
		Centre (DIFC) and a former Minister and Central Bank First Vice-Governor 
		in Lebanon, commenting on the survey said: “The MasterIndex survey 
		participants are generally optimistic about the prospects for the 
		economy, employment, their regular income, the stock markets and the 
		quality of life in the countries of the region.”<br>
		<br>
		Real GDP for the region* has expanded by some 6.5-7 per cent during 2006 
		and the growth is expected at similar rates in 2007. Consumers’ 
		perception of the economy is, in most cases, in line with the actual 
		economic growth outcomes and is reflecting the ‘feel good’ factor for 
		the economy, regular income and employment, Dr Saidi added.<br>
		<br>
		Overall confidence across the MEL region improved slightly in the latest 
		survey with average confidence increasing from 82.1 six months ago to 
		83.9. Since MasterIndex began in 2004, overall consumer
		confidence in MEL has increased steadily from the first average score of 
		66.1 and currently has an historical average of 75.2, said the survey.<br>
		<br>
		<b>MIT’s plan for ‘Smart City’</b><br>
		King Abdullah Economic City (KAEC), now a Tadawul-listed company, said 
		it has held discussions with the Massachusetts Institute of Technology 
		(MIT) to help in developing KAEC as the first ‘Smart City’ in the world.<br>
		<br>
		KAEC, the single-largest private sector development in Saudi Arabia, has 
		six key components – a Seaport, Industrial District, Central Business 
		District featuring a Financial Island, Educational Zone, Resort District 
		and Residential Zone. The Saudi Arabian General Investment Authority 
		(SAGIA) is the prime facilitator of the project that spreads over 168 
		million square metres along the Red Sea coast.<br>
		<br>
		The MIT team will help KAEC integrate all its services through one call 
		centre. “All modern conveniences in telecommunication, including mobile 
		connectivity, fibre lines, video conferencing facilities and wireless 
		Internet solutions will be offered at KAEC,” said Mr Nidal Jamjoom, CEO 
		of Emaar.E.C., who met with the MIT delegation to discuss strategies to 
		shape KAEC as the world’s first truly IT-driven ‘Smart City’.<br>
		<br>
		“Other e-driven solutions include e-logistics support of traffic and 
		fleet management; e-security solutions including whole city monitoring; 
		e-commerce facilities; e-media including cable television and electronic 
		billboards; telemedicine facilities; e-education options; smart home 
		integration and e-climate management in the form of smart reactions to 
		city climate,” said Mr Jamjoom, who added that KAEC will also have a 
		feedback module that enables to interact with the environment and people 
		swiftly.<br>
		<br>
		The IT team of Emaar.E.C, led by Omar Al-Khudairi, Senior Manager, the 
		project’s development team and the IT team from Emaar Properties, 
		attended the discussions. “Our association with MIT will serve as a 
		technology telescope for bringing in emerging technologies and roll out 
		ICT solutions that will continue to uphold the status of KAEC as the 
		world’s ‘Smart City’ even after several years,” said Mr Al Khudairi.<br>
		<br>
		<b>CPC plans Riyadh industrial zone</b><br>
		Construction Products Holding Company (CPC), a newly formed Saudi closed 
		joint stock company, announced plans to establish an industrial zone in 
		Riyadh. Dr Faysal Alaquil, CPC’s director of Business Development, said 
		the 200,000-square metre industrial complex in Riyadh illustrates that 
		Saudi Arabia remains the foremost market for the company despite its 
		regional expansion.<br>
		<br>
		CPC also plans to transfer the group’s existing construction-related 
		factories under one roof. The group has purchased an 840,000-square 
		metre land in Bahra, located between Jeddah and Makkah, to serve as a 
		base for all its building and construction products. Bahra Company for 
		Construction Steel covers an area of 40,000 square metres.<br>
		<br>
		The presence of all the factories in one industrial area will facilitate 
		the provision of supporting services and reduce the cost of managing, 
		thus strategically realigning CPC through expansion in the industrial 
		and construction markets, said the company. The CPC recently entered 
		into an agreement with Arkan Building Materials (ARKAN) of Abu Dhabi to 
		establish six limited liability factories for producing building 
		materials, besides a transport company in Abu Dhabi. CPC has also 
		purchased a 265,000-square metre lot in the industrial zone of Adra, 
		Syria, for building an industrial complex. </font></p>
