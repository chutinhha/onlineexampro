</font><font face="Verdana" size="2" color="#CC3333"><b>Symantec launches Norton 360</b></font><font face="Verdana" size="1"><br>
<img border="0" src="scan2.jpg" width="100" height="126" align="left" hspace="6" vspace="4">Symantec, the world’s largest PC antivirus maker, has unleashed Norton 360, the 
company’s latest salvo in the battle for consumer security dollars. Norton 360 
is Symantec’s new flagship consumer security product, unseating its Norton 
Internet Security suite. First announced a year ago under the Genesis code name, 
Norton 360 includes a host of technologies, many from Symantec’s current 
security, PC optimisation and backup products. Yet Symantec said it didn’t just 
cobble together existing wares for Norton 360; many of the features are 
brand-new. “It is all-in-one security for the mainstream PC user who wants a 
product that is going to take care of things for them,” said Tom Powledge, 
director of product management at Symantec. “We didn’t want to make it a big old 
kitchen sink. We’re not playing the feature game. We want to make the features 
right for every customer.”<br>
<br>
Norton 360 is designed to compete with the latest products from McAfee as well 
as security newcomer Microsoft, which shipped Windows Live OneCare last May. 
Following Microsoft’s lead, all of the latest security products combine basic 
antivirus, antispyware and firewall technologies with backup features and 
several tune-up tools for Windows PCs – features Symantec and McAfee 
traditionally charged extra for.</font><hr color="#C0C0C0" width="90%" size="1">
