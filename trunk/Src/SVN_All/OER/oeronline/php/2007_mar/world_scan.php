<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2" color="#CC3333"><b>Siesta good for your heart</b></font><font face="Verdana" size="1"><br>
Sleeping a little over half-an-hour in the middle of the day may reduce the risk 
of heart diseases, particularly in healthy young men, say researchers. 
Mid-afternoon naps are often taken after a mid-day meal. Such a period of sleep 
is a common tradition in countries with hot climate. Dimitrios Trichopoulos from 
the Harvard School of Public Health and other researchers looked at 23,681 men 
and women aged between 20 and 86 who did not have a history of heart disease or 
any other severe condition. The six-year-long Greek study took into account ill 
health, age and physical activities of the participants, who were asked if they 
took mid-day naps and how often.<br>
<br>
They were also asked about their dietary habits. The researchers found that 
those who took naps of any frequency and duration had a 34 per cent lower risk 
of dying from heart disease than those who did not take mid-day naps. Those who 
took naps of more than 30 minutes, three or more times a week, had a 37 per cent 
lower risk. Among working men who took mid-day naps, there was a 64 per cent 
reduced risk of death compared with a 36 per cent reduced risk among non-working 
men. Experts said napping during the afternoon might help people relax by 
reducing their stress levels. The researchers added that if backed by other 
trials, taking a siesta would be an interesting way of reducing heart disease, 
as it had no side effects. The only factor was that people should not reduce the 
amount of physical activity they did during the rest of the day. 
