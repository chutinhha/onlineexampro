</font><font face="Verdana" size="2" color="#CC3333"><b>Intel builds fastest chip</b></font><font face="Verdana" size="1"><br>
Computing took a leap forward when chipmakers started putting more than one core 
– or central brain – on a single chip. It was a way to make machines work harder 
even as they consumed less power. Now, chip-making giant Intel (INTC) has said 
it has successfully produced a chip with 80 cores, the size of a fingernail, 
capable of processing a mind-boggling 1 trillion calculations a second.<br>
<br>
The chip, which Intel claims is the fastest ever made, could start being used 
commercially in “five years, if not sooner.” The massive processing power each 
chip would provide will dramatically change the way consumers and businesses 
work and play. For example, financial analysis that takes days to perform in 
back offices could be done in seconds at a trader’s terminal on Wall Street. The 
test chip, which does not yet use the standard x86 architecture common to most 
PCs and servers, consumes an average 62 watts of energy—less than some chips on 
the market today. It also takes a novel approach of stacking memory in three 
dimensions directly on top of the chip, creating an architecture that would 
transfer information at lightning speed.<br>
