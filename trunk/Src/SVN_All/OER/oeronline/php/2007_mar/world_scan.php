</font><font face="Verdana" size="2" color="#CC3333"><b>Euro Zone on a six-year high</b></font><font face="Verdana" size="1"><br>
Economic growth in the countries that use the euro jumped last year and reached 
its highest annual rate in six years. The EU statistics office Eurostat 
estimates that GDP expanded by 2.7 per cent, nearly double the 1.4-per cent 
expansion seen in 2005. The European Commission has already said it will 
probably raise its current forecast of 2.1 per cent growth in 2007. Growth 
rebounded in last three months of 2006 and reached 0.9 per cent, the same as 
during the second quarter and a major improvement on the 0.5 per cent of the 
third quarter. Germany enjoyed 2.7 per cent growth last year compared with 0.9 
per cent in 2005. The growth rate in France rose from 1.2 per cent to 2 per 
cent. Italy – where the economy had stagnated in 2005 – also managed a 2 per 
cent expansion. The region’s growth does not appear to have been damaged by the 
European Central Bank’s interest rate increases.<br>
<br>
Moreover, the latest figures make it more likely that there will be another rise 
in March. The European Commissioner for Economic and Monetary Affairs, Joaquin 
Almunia, said the economy is coping well with the euro’s strengthening against 
the dollar and the yen.<br>
