<font face="Verdana" size="1">WPP on expansion spree in Middle East<br>
<br>
WPP Group Plc, the world’s second biggest marketing services company, is getting 
aggressive in the Middle East. Group CEO Martin Sorrel recently stated that the 
group is seeking to expand its equity interests in the Middle East by acquiring 
more shares in its regional partners and by buying into more agencies. He made 
it clear that his goal is to acquire controlling stakes in WPP’s partnerships in 
the region. WPP has partnerships with local companies in ad agencies, including 
Bates Pan-Gulf Group, Grey Worldwide, Memac Ogilvy &amp; Mather, JWT MENA, Team Y&amp;R; 
media agencies including Mediaedge CIA and Mediacom, and the regional branch of 
PR agency Hill &amp; Knowlton. It also has indirect stakes in the regional offices 
of media agencies Mindshare and Media Insight, the 50-50 joint ventures of Memac 
Ogilvy and JWT MENA. As per market reports, Sorrel is negotiating with one or 
more agencies for acquisition in the region. The expansion of Ogilvy Healthworld 
in the region is also on the cards.<br>
<br>
‘Windows’ on the Web<br>
The web is where it is all happening – or so internet pundits tell us. The 
applications that we use on our desktop or laptop personal computers will 
eventually all migrate to the web, they say. Indeed, players such as Google 
already offer their users the ability to create and save documents and 
presentations entirely through a browser. But it’s still something happening 
piece-meal and in driblets. Now, an India-based start-up, fuelled by the 
creative efforts of a dozen young Indian programmers, has boldly gone where no 
major player has gone before. It has just announced the ‘world’s first 
Windows-based online desktop.’ It’s called Nivio and helps one to create a 
full-fledged, but entirely virtual, desktop of one’s own – on the web. Those who 
register for a trial of the beta or advanced prototype get the full Windows XP 
look and feel; 5 gigabytes of storage, and all the standard desktop features 
such as file back-up, virus and spam protection. The free bundle of software 
tools includes the OpenOffice suite, Outlook Express email tool and the Internet 
Explorer browser. Also on offer are Acrobat 8 reader, iTunes and Winamp players, 
Yahoo Messenger, and for open software lovers, the choice of a Mozilla/Firefox 
browser and the Foxit Reader.<br>
Airline industry profitability improves<br>
The International Air Transport Association (IATA) has announced a US$5 billion 
projected profitability for airlines in 2007. IATA Director General and CEO 
Giovanni Bisignani said, “The industry has changed tremendously in five years. 
Labour productivity is up 56 per cent. Distribution costs are down 13 per cent. 
Non-fuel unit costs dropped 15 per cent and load factors are at record highs – 
76 per cent in 2006. Airlines needed an oil price of less than US$20 per barrel 
in 2002 to break even. Today we are profitable at nearly US$70 per barrel.” 
While the results are encouraging, Bisignani also sounded a word of caution. 
“Airlines are a US$470-billion industry. A profit of US$5 billion is peanuts. We 
need US$40 billion just to cover the cost of capital. The industry is moving in 
the right direction, but with US$200 billion of debt, the financial hole is 
deep. The challenge is to turn the peanuts into sustainable profits.”<br>
Global energy consumption continues to grow<br>
The year 2006 saw high and volatile energy prices. But despite high prices, 
world energy consumption growth remained above average in keeping with recent 
trends. Energy use is also increasingly shifting away from OECD countries and 
becoming more carbon-intensive. These are the main findings from the BP 
Statistical Review of World Energy 2007. It was a year when energy markets were 
once again the centre of attention, attracting the interest of politicians, 
consumers and policy-makers alike. “Last year showed markets at work. Primary 
energy consumption growth has decelerated – particularly for fuels which have 
seen the highest increase in price,” said BP’s chief economist-designate 
Christof Rühl. “However, global carbon intensity – the link between carbon 
emissions growth and energy growth – has increased.” For the second year in a 
row, world energy growth slowed, rising by 2.4 per cent, down from 3.2 per cent 
in 2005, but still just above the 10-year average. The pattern of recent years, 
which has seen robust demand in Asia Pacific and China in particular, was 
repeated with Chinese energy consumption rising more than 8 per cent. China’s 
usage of all forms of energy rose in the year, taking the country’s share of 
total global consumption to more than 15 per cent.<br>
US economy growth slows down to 0.7 per cent in Q1 <br>
The US economy grew at an annual rate of 0.7 per cent in the first quarter of 
2007, much slower than the 2.5 per cent pace in the final quarter of last year, 
marking the weakest showing in more than four years. The growth rate in real 
gross domestic product – the output of goods and services produced by labour and 
property located in the United States – was slightly higher than the 0.6 per 
cent pace previously estimated. The final reading disappointed economists. The 
deceleration in real GDP growth in the first quarter primarily reflected an 
upturn in imports, a deceleration in exports, a downturn in federal government 
spending, and a deceleration in personal consumption expenditures for <br>
non-durable goods. <br>
