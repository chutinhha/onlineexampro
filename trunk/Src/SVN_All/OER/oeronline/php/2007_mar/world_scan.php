</font><font face="Verdana" size="2" color="#CC3333"><b>Loss forces Coca-Cola to slash 3,500 jobs<br>
</b>
</font><font face="Verdana" size="1">
<img border="0" src="scan1.jpg" width="75" height="133" align="right" hspace="6" vspace="4">Coca-Cola Enterprises Inc., the world’s largest soft-drink distributor, will cut 
3,500 jobs after posting its worst loss in a decade. The fourth-quarter loss was 
$1.71 billion, or $3.59 a share, the Atlanta-based bottler said in a statement. 
Most of the cuts, as part of the restructuring of the company’s North American 
operations, will come from not filling open positions, spokeswoman Laura Asman 
said. Coca-Cola Enterprises employs 73,000 workers. Coca-Cola CEO John Brock 
struggled to halt soda declines in the US as consumers switch to ‘healthier’ 
drinks. The bottler raised prices in North America by 3 per cent in response to 
higher costs of aluminium and sweetener. That caused volumes to drop by 2.5 per 
cent, the worst in almost two years for the unit.<br>
<br>
The company took a non-cash charge of $3.80 a share to reduce the value of its 
franchise licenses for bottlers it had purchased more than a decade ago. The 
company forecast 2007 earnings per share to fall between 5 and 10 per cent as 
compared to $1.30 a share in 2006. That indicates a profit next year of between 
$1.17 and $1.24 a share. Coca-Cola Enterprises will save $50 million to $100 
million in 2007, and an additional $100 million to $125 million by 2008 because 
of the job cuts and restructuring, Chief Financial Officer Bill Douglas said.</font><hr color="#C0C0C0" width="90%" size="1">
