<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Re-Branding Dubai<br>
</font></b><font face="Verdana" size="1"><b>Dubai is positioning itself as a cosmopolitan powerhouse and is working on a 
holistic plan encompassing infrastructure, economic and social development, 
security and public sector excellence</b><br>
<br>
<img border="0" src="closeup.jpg" width="149" height="284" align="left" hspace="6" vspace="4">All eyes are now on the Dubai Strategic Plan (DSP), which aims at transforming 
Dubai into a major economic power in the region. Over 2007-2015, the DSP is in 
fact helping in branding the emirate. If all goes according to the schedule, 
Dubai’s gross domestic product (GDP) should cross the US$100 billion mark by 
2015.<br>
<br>
Through DSP, Dubai is positioning itself as a cosmopolitan city. It is branding 
itself as a place where people of diverse cultural backgrounds can work and live 
side by side. Undoubtedly, Dubai likes to attract high net worth individuals 
from the region and beyond who are looking for a place offering state-of-the-art 
amenities, and is prosperous, safe and sound.<br>
<br>
DSP has the support of the highest authority in the emirate. Shaikh Mohammed Bin 
Rashed Al Maktoum, Vice-President and Prime Minister of the UAE and Ruler of 
Dubai, unveiled the ambitious plan in early February. Hundreds of invited 
guests, including government officials, business leaders and academicians, 
attended the gathering where Shaikh Mohammed explained the logic behind the 
venture. By definition, DSP is part of a broader plan of the United Arab 
Emirates. However, the UAE is yet to disclose the plan for the entire country. 
Thus, it seems, pacesetter Dubai has decided to make the first move on the 
behalf of the country.<br>
<br>
<b>Ambitious goals</b><br>
DSP’s goals are ambitious yet attainable by Dubai standards. In economic terms, 
the initiative aims at increasing Dubai’s GDP from US$37 billion in 2005 to 
US$108 billion by 2015. Also, the plan calls for raising annual per capita 
income from US$33,000 in 2005 to US$44,000 in 2015. Additionally, it intends to 
register an average of 11 per cent annual growth in GDP.<br>
<br>
Dubai is known for beating its own records. The GDP and per capita income 
estimated in 2005 were original goals set out for 2010, as part of a plan spelt 
out in 2000. The chances are that Dubai would achieve the DSP targets earlier 
than planned.<br>
<br>
The plan calls for focusing energies on sectors where Dubai can attain 
competitive advantages. These sectors are tourism, transport, trade, 
construction and financial services. Non-oil sectors thus will be the key in 
shaping Dubai’s economy in the years to come. Already, the services sector 
accounts for three-quarters of Dubai’s GDP.<br>
<br>
For example, Dubai is not being presented as a primary manufacturing place 
simply because the emirate lacks the necessary skilled human resources and cost 
advantages compared to China, for example. At the moment, Bahrain rather than 
Dubai serves as the leading financial services hub in the region. Dubai is 
trying to correct this through the Dubai International 
Financial Centre (DIFC). The US$2 billion DIFC is Dubai’s answer to the US$1.3 
billion Bahrain Financial Harbour (BFH). Both DIFC and BFH are being developed 
in phases.<br>
<br>
To achieve integrated growth, the DSP is divided in five sections – economic 
development; social development; infrastructure, land and environment; safety, 
security and justice; and, public sector excellence. Each sub-plan is designed 
to achieve a distinct set of objectives.<br>
<br>
<b>Not reliant on oil </b> <br>
By and large, the petroleum sector is not uniquely important in Dubai’s GDP. 
According to Shaikh Mohammed, non-oil sectors accounted for 97 per cent of 
Dubai’s GDP in 2006 versus 90 per cent in 2000 and 46 per cent in 1975. The 
petroleum sector though remains the main source of revenue for budgetary income. 
Oil accounts for a significant portion of Dubai’s exports.<br>
<br>
As far as social development is concerned, the plan calls for preserving 
national identity. This is considered vital, as foreign nationals constitute the 
majority of people working and living in the emirate. In fact, the issue of 
foreign representation is an emotional one in Dubai and the emirate stands out 
as being uniquely receptive to people from different countries. It is estimated 
that people from more than 100 countries work and live in Dubai, astonishing by 
any standards.<br>
<br>
The plan embraces cultural activities through raising awareness of local 
culture. Increased cultural activities will also contribute into making Dubai a 
livelier place to stay. Dubai, in fact, already holds extravaganzas like the 
famous Dubai Shopping Festival.<br>
<br>
The DSP also underscores the significance of ensuring quality healthcare and 
educational services throughout the emirate. Emphasis on health and education 
should help improve UAE’s performance in the annual Human Development Index (HDI), 
issued by United Nations Development Programme (UNDP). The UAE emerged No. 46 
worldwide in the 2006 HDI report. This marked the fourth-best performance 
amongst Gulf Cooperation Council (GCC) countries, namely Kuwait, Bahrain and 
Qatar, respectively.<br>
<br>
<b>Ensuring better life<br>
</b>With respect to the infrastructure, land and environment, Shaikh Mohammed 
assured his audience that the authorities are determined to address the issue of 
traffic bottlenecks in Dubai. Work is underway to develop a metro system linking 
key areas of the emirate via light rail. In the next few years, Dubai will 
become the first place in the GCC providing solutions to mass transit problems.<br>
<br>
The government has also committed itself to ensuring energy sources, notably 
electricity. This is vital in the light of growing demand for utilities 
following diverse development projects undertaken by public and private sector 
enterprises.<br>
<br>
Safety, security and justice form an important part of DSP. Police officials 
intend to enhance safety for all people living and visiting Dubai. Like London, 
Dubai relies heavily on installed cameras for tracking violators of public 
order. Also, DSP stresses the importance of ensuring safety at working places. 
It is absolutely vital that officials send a clear message to owners of 
properties and other projects that there would be no tolerance of those who put 
at risk the safety of their employees.<br>
<br>
As part of efforts to ensure justice, the authorities intend to speed up 
implementation of court orders once verdicts are issued. Certainly, it is one 
thing to issue a ruling, but totally another to implement the order. This should 
help addressing grievances raised by the society, notably the business 
community.<br>
<br>
DSP also focuses on public sector excellence. The plan stresses the need to 
strengthen accountability and transparency in governmental departments. In fact, 
this is already underway as the UAE enjoyed the best performance amongst GCC 
countries in the 2006 Transparency International report. The UAE was ranked 31 
amongst 163 countries featuring in the report. <br>
<br>
Not resting on its laurels, the plan calls for emphasis on e-government in 
handling basic matters, including complaints. Handling matters electronically 
would be more efficient, which in turn would lead to more savings. <br>
<br>
Dubai’s traditional formula focused on the government assuming the leading role 
in investing on infrastructure and major projects such as Dubai Media City and 
Dubai Internet City. Private sector firms and entrepreneurs tended to build on 
governmental efforts. Shaikh Mohammed has assured his audience that Dubai 
government intends to continue playing this role.<br>
<br>
<b>Dubai World Central<br>
<img border="0" src="closeup1.jpg" width="300" height="198" align="right" hspace="6" vspace="4"></b>One such project that illustrates the concept of the emirate serving as a global 
city is that of Dubai World Central (DWC). Viewed as a city within a city, DWC 
is being developed in an area of 140 sq. km and will cost a whopping US$33 
billion. On completion, it will add nearly 900,000 inhabitants to the city 
(currently Dubai has a population of 1.35 million). <br>
<br>
The government launched DWC in May 2006 with no specific deadline for 
completion. However, some key areas of the project should be operational in the 
next 10 years or so. The project includes an airport, capable of handing 120 
million passengers and 12 million tonnes of cargo annually. Upon its completion, 
the airport will be one of the largest, if not the largest, airport in the 
world. Officials believe that Dubai is well positioned to serve as a central 
focal point for numerous countries in the region as many cities are located at 
within three to four-hour flying distance.<br>
<br>
Other places too can benefit from Dubai’s drive of becoming a global city. The 
spillover effects provide opportunities for countries like Oman, notably the 
industrial areas in the Sultanate. GCC rules allow industrial products from Oman 
to enter the UAE with no restrictions or customs duties. Clearly, the sky is the 
limit as far as the DSP, and Dubai, is concerned.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
