<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4"><b>Towards A Digital Society<br>
</b></font><font face="Verdana" size="1"><b>By recognising Oman’s web talents, 
the Oman Web Awards 2006 was an important milestone encouraging online 
innovation and technical knowledge</b></font><p align="justify" style="margin: 3px 10px">
<br>
<font face="Verdana" size="1">
<img border="0" src="talenthunt.jpg" width="300" height="205" align="left" hspace="6" vspace="4">The 
Oman Web Awards 2006 concluded at a scintillating award ceremony on February 16, 
2007, honouring the best of the Omani websites across a range of sectors. 
Instituted in 2005, the Oman Web Awards has been guided by two fundamental 
objectives. First, to utilise the event to promote and develop Omani talent; and 
secondly to encourage all sectors to showcase their websites and be more 
involved in the digital economy.<br>
<br>
With all winners entering Pan Arab Web Awards, the event has taken Omani web 
talent to a global forum. Oman Web Awards 2006 was held under the auspices of 
Information Technology Authority (ITA); presented by Advanced Business Solutions 
LLC; powered by Gazelle International; sponsored by Oman Cables Industry SOAG 
and supported by Business Software Alliance and the strategic Media Partner Oman 
Economic Review.<br>
<br>
The launch for Oman Web Awards 2006 took place in November and there were about 
100 entries in 17 categories with online registrations. There was a tremendous 
response in all the categories especially automotive (a new category added this 
time), banks and financial institutions, corporate, forum, travel and tourism 
etc. Unlike the last year, there were potential entrants and emerging winners 
for the forums, media and press categories.<br>
<br>
The tremendous response in registrations and support for the award is an 
indication of the significance of its merit to the community and the prominence 
of the honour within the web design industry. The standardised criteria by Pan 
Arab Web Awards committee to evaluate the websites are concept/creativity (20 
per cent), technical/ease of use (25 per cent), content/structure (25 per cent), 
visual design solution/aesthetics (20 per cent) and interactivity (10 per cent).<br>
<br>
As per jury representative Madhulika Mohta, Manager, Consultancy, at BDO Jawad 
Habib, “Oman Web Awards is a good platform for upcoming web designers and 
entrepreneurs to showcase their work nationally and internationally and to know 
the latest technologies and standards involved in the web industry.” As the 
validator of the jury, Sangeetha Sridhar, an eminent technology columnist and 
Content Consultant at the ITA, opined that the entire evaluation process had 
been just and fair giving equal opportunity for all entrants to present their 
best efforts in web design and enhance the impressive online presence in Oman, 
adding, “The standard of the online presence of Omani organisations has been 
raised, thus contributing to the creation of the digital society of Oman.”<br>
<br>
The glittering award ceremony was held at Crowne Plaza under the auspices of ITA, 
with Dr. Salim Al Ruzaiqi, CEO of ITA as the guest of honour. The President of 
Pan Arab Web Awards Pierre Moukarzel was also present to grace the ceremony. The 
function started with Ali Jaffer Abdul Rahim, Chairman of Oman Web Awards and 
Managing Director of Advanced Business Solutions LLC, welcoming the guests.<br>
<br>
The award trophies were presented by dignitaries that included Dr. Salim Al 
Ruzaiqi, CEO, ITA; Pierre Moukarzel, President of Pan Arab Web Awards; Ali 
Jaffer Abdul Rahim, Chairman of Oman Web Awards and Managing Director, Advanced 
Business Solutions LLC; Mohammed Mustafa Al Lawati, Assistant General Manager of 
Oman Cables Industry SAOG; Sandeep Sehgal, General Manager, United Media 
Services; Amit Sampat, Director of Gazelle International; Devang Sampat, 
Director, Finance, Naranjee Hirjee Co LLC; and Nikhil Sampat, Director 
Marketing, Naranjee Hirjee Co LLC.<br>
<br>
With Tariq Al Barwani as Master of the Ceremony, Philip, the gifted magician, 
and the dazzling Salsa dancers, the award ceremony added energising 
entertainment to the spirited audience. The Pan Arab Web Awards committee 
announced that from next year on there would be an award for the ‘Best Designer 
of the Year’, which will go to an individual or company that has designed most 
of the award-winning websites. More details about the 55 winners across all 
categories are also available at
