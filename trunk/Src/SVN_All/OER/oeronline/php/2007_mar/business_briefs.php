of 285 metres and an alongside depth of 16.5 metres. The terminal has 4 post-Panamax 
quay cranes, 8 Rubber-tyred Gantry Cranes (RTGCs), 2 Reachstackers, and a fleet 
of 15 tractors and 33 trailers. Speaking at the ceremony, Meredith said, “OICT 
is an important member of the HPH network of ports. With its strategic location 
outside the Straits of Hormuz, OICT will strengthen Oman’s position as a centre 
of trade as well as serve as a gateway to international trade for the Sohar 
Industrial Port and other developing industries in the Batinah region.”<br>
