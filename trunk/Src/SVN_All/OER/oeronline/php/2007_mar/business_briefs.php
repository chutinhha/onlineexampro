</font><font face="Verdana" size="2" color="#CC3300"><b>OICT opens Terminal B</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bus1.jpg" align="right" hspace="6" vspace="4">Oman International Container Terminal (OICT) recently celebrated the opening of 
its Terminal B, Phase I at Sohar Port. The ceremony was marked by HE Sheikh 
Mohammed bin Abdullah Al Harthy, Minister of Transport &amp; Communications, and 
John Meredith, Group Managing Director, Hutchison Port Holdings (HPH), who 
jointly unveiled a plaque on the occasion. Addressing the gathering, HE Sheikh 
