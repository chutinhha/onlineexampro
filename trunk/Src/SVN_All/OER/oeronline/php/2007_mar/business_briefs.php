<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">PDO to invest in social projects</font></b><font face="Verdana" size="1"><br>
The Petroleum Development Oman (PDO) has signed four Memoranda of Understanding 
(MoUs) with three government agencies and a semi-governmental organisation as 
part of its social investment programme. As per the MoUs, PDO would provide 
financial support for four separate projects that include the development of the 
Al Hujira craft centre in Al Suwaiq, the provision of entrepreneur training in 
Dhofar, funding of an awareness campaign for the Environment Society of Oman (ESO), 
and funding a Palaeolithic survey in central Oman. The Al Hujira Craft Centre 
will create jobs for women and help create new markets for Omani handicrafts. 
The small-business training that PDO is supporting is being organised by the 
Dhofar Governorate. The aim is to train a total of 50 people to set up and 
operate small businesses. PDO’s support for the ESO will allow the society to 
hire a ‘champion’ to undertake environmental awareness work in Muscat. The 
support for the Palaeolithic survey of central Oman will help develop a better 
understanding of Oman’s ancient history. The project will involve a systematic 
survey of the Huqf area with the aim to produce an archaeological map of the 
region. “Today’s social investment MoUs are another example of PDO’s ongoing 
commitment to support the vocational, environmental and cultural projects in the 
Sultanate,” PDO Managing Director John Malcolm said. <br>
