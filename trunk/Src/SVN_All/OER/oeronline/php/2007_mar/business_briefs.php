</font><font face="Verdana" size="2" color="#CC3300"><b>Seeb Airport expansion 
underway<br>
</b></font><font face="Verdana" size="1">
<img border="0" src="bus.jpg" width="150" height="113" align="left" hspace="6" vspace="4">HE Sheikh Mohammed bin Abdullah Al Harthy, the Minister for Transport and 
Communications, recently met Omani businessmen and private sector 
representatives in the presence of Mohammed bin Sakhar Al A’amri, Undersecretary 
of Ministry of Transport and Communications for civil aviation affairs. The 
minister emphasised the importance of the Seeb and Salalah airports’ development 
being implemented by the Sultanate within the context of the current development 
plan to cope with the growth of economic and tourism movement in the Sultanate. 
He sought the Omani private sector’s contribution in the project implementation. 
The development has been designed, keeping in mind the latest developments in 
the travel industry, construction and design world, creating harmony between the 
two airports’ new buildings and the environment surrounding them. The minister 
said that Seeb airport development project comprises four phases. Work on Phase 
1 has commenced and is due to be finished in four years, with the airport 
expected to accommodate up to 12 million passengers annually. On completion of 
the fourth phase, the total capacity of the airport is envisaged to touch 48 
million passengers. The new airport comprises a new passenger terminal, runway, 
cargo services and maintenance building, shopping centres in addition to a 
multi-purpose lounge. The passenger capacity of Salalah airport is expected to 
reach two million annually. On his part, Eng. Salim bin Said Al Ghattami, OCCI 
chairman, said that private sector firms and establishments were keen to follow 
up development of Seeb and Salalah airport projects and were identifying 
available opportunities for mutual benefit.<br>
