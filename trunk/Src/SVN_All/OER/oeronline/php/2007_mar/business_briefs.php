</font><font face="Verdana" size="2" color="#CC3300"><b>Islamic Finance Forum Meet in April</b></font><font face="Verdana" size="1"><br>
The 12th International Islamic Finance Forum (IIFF), which is to take place at 
the Jumeirah Beach Hotel, Dubai from April 2-5, 2007, will explore potential 
strategies for growth and diversification by focusing on a recent research study 
conducted by international consultancy firm KPMG. The study highlights a number 
of key issues, such as acquisitions in the conventional banking sector, to 
broaden its access to the estimated 1.5 billion people globally with 
unprecedented levels of liquidity. To put the hydrocarbon riches into 
perspective, the combined wealth of 300,000 millionaires in the Middle East is 
estimated to be US$1.4 trillion. With Kuwaiti investors currently bidding for 
Malaysian banks, the creation of an Islamic ‘mega-bank’ is a distinct 
possibility and underlines the industry’s aspirations to develop a truly global 
aspect.<br>
<br>
IIFF Dubai has an unrivalled line-up of over 50 regional and international 
expert speakers, plus 20 hours of networking for Islamic finance professionals, 
scholars and academics from both private and public sectors.<br>
