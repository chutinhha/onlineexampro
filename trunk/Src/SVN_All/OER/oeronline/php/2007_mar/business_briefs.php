</font><font face="Verdana" size="2" color="#CC3300"><b>Oman-Bulgaria ink Investment Pact</b></font><font face="Verdana" size="1"><br>
Oman and Bulgaria have signed an agreement for encouragement and mutual 
protection of joint investments between the two countries. HE Ahmed bin 
Abdulnabi Macki, Minister of National Economy and Deputy Chairman of the 
Financial Affairs and Energy Resources Council, signed the agreement on behalf 
of the Sultanate’s Government while Roumen Ocharov, Minister of Economy and 
Energy, signed it on behalf of the Bulgarian side. Macki said that agreement 
would encourage investments and provide the necessary legal and legislative 
framework ensuring mutual protection of the interests of both sides. He said the 
agreement would protect investor’s rights against non-commercial hazards in a 
manner that will encourage increase in of volume of joint investments in public 
and private sectors. He also emphasised the importance of exchange visits 
between public and private sectors’ officials in order to examine experiments in 
various spheres, create economic and trade cooperation that would benefit both 
the Sultanate and Bulgaria. On his part, the Bulgarian economic and energy 
minister hailed the Sultanate’s progress and prosperity in various spheres, 
emphasising his country’s keenness to promote relations between the two 
countries.<br>
