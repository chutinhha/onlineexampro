</font><b><font face="Verdana" size="4">The Enchanting Drama</font></b><font face="Verdana" size="1"><br>
<br>
<b>I hear, I forget;<br>
I see, I remember;<br>
I do, I understand.<br>
<br>
- </b>Chinese Proverb<b><br>
</b><br>
<br>
<img border="0" src="manag6.jpg" width="200" height="354" align="left" hspace="6" vspace="4">The 
world is in a swirl. We are not delving into the political realm, but the 
corporate domain. Globalisation and shareholder activism are peaking in the 
world’s richest theatre, the United States. As a corollary, managements are 
under pressure. Performance is the key to success, nay survival of the occupants 
of the corner rooms in the corporate corridors. No wonder, tomes are being 
written, and sold, on the subject of management in as many languages as one can 
imagine. While some management gurus dish out their knowledge on the basis of 
pure theoretical terms, there is a school of thought that culls out its 
learnings through empirical studies: by watching the managers in ‘play’, 
performing their parts with their goals in sight – with the willing consent of 
other actors, the managers.<br>
<br>
The notion of born leaders appears to have lost its charm these days. Leaders, 
if management gurus are to believed, can now be grown or cultivated from 
scratch. There lies the tale of growing importance of Management Development 
Programmes (MDP). The medium of these MDP programmes has also witnessed 
tremendous change. From the traditional sleep-inducing seminars to coaching, the 
package in which these programmes are being offered has undergone profound 
transformation. <br>
<br>
The buzz today seems to be on leadership development in the form of theatre 
representation. Even though we know that transformational leaders don't grow 
like weeds in the corporate garden, it is a fact that they can be cultivated if 
the organisation offers the right leadership development programmes and make 
leadership development a priority. <br>
<br>
Traditional forms of MDPs include leadership development in the form of 
speeches, leadership development approach and leadership development coaching.<br>
<br>
The new form of MDP is, however, Leadership development through Theatre 
representation. It is a teaching medium that employs a live performance or 
reading of an original dramatic scenario based on a true-life incident. Its 
purpose is to serve as a ‘professional development tool’ for viewers and 
participants to reflect their own personal praxis onto that depicted in the 
scenario. <br>
<br>
This unique approach to leadership training is increasingly becoming popular. As 
one of most recent innovations in the category of knowledge management 
initiatives, it combines the power of a medium like theatre with effective 
classroom training – by using the tried and tested story telling technique.<br>
<br>
Today, increasingly, a theme is being woven in the context of Motivation/ 
Leadership and Team Building around the persona of ‘regional heroes’, and 
utilised as theatre-tuition even in educational institutes. This process is 
totally experimental and also great fun, while enabling deep learning. Most 
learners find the programme effective and feel it them make important decisions. 
They also strongly believe that theatrical plays are very powerful medium for 
MDPs, as they not only train the mind subliminally but make such learning a more 
joyful experience, as they are more interesting than most conventional run of 
the mill programmes. <br>
<br>
The power of story telling is increasingly being recognised by professionals as 
applicable to business management, knowledge management, organisational 
development and coaching. It ignites action in knowledge-based organisations. 
This medium aims at taking away the non-relevant and non-practical aspects of 
attending seminars. It engages as a first-level impact medium, retaining the 
flavour of these important aspects:<br>
<br>
&#8730; Application<br>
&#8730; Relevance in the context of corporate context.<br>
&#8730; Providing the opportunities for development<br>
&#8730; Stimulating the ability to develop (including motivation, skills and knowledge 
for change)<br>
&#8730; Providing a supportive context for change to occur</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">Today, theatrical plays have gained significant 
importance as they are seen to be participatory and often result in evoking the 
right response in the learner. Participants do not feel that they are being made 
mandatory to attend. The end result: it raises the aspirations of followers and 
creates a star within each and every individual.<br>
<br>
<b>Ramesh Kumar</b><br>
