                  <td width="100%"><img border="0" src="../../images/2001_may_june/golf.jpg" align="left" hspace="5" vspace="5" width="116" height="291"><font face="Verdana" size="4" color="#FF0000"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
                    </font></font>
                    <font face="Verdana" size="4" color="#FF0000">Golf of Oman</font>
                    <p><font face="Verdana" size="1"><b>The construction of Oman's first green golf course will not only boost the tourism sector, it will put the Sultanate on the golfing world map, writes OER Editor Matthew Brockett in this exclusive report.&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2">The year is 2005, and Tiger Woods is lining up an impossible 17-foot birdie putt on the 18th green. The air is thick with tension as Woods, four-time Masters Champion and living sports legend, prepares to send the ball across the lush green grass towards the distant flag.&nbsp;<br>
                    At that moment, the eyes of the world are on him. They are also on Oman.&nbsp;<br>
                    If Woods sinks the putt, he will win the inaugural Oman Arabian Open - the Sultanate's first international golf tournament held on its first green golf course.<br>
                    If he misses, he will lose the tournament, but Oman will still be a winner.<br>
                    <br>
                    Golf is far more than just a game. It is a multi-billion dollar industry, intrinsically linked to big business and a key driver in many countries' tourism sectors. Its growing popularity, and the advent of superstars like Woods, has turned the humble golf course into a world stage.<br>
                    <br>
                    Witness the Dubai Desert Classic earlier this year, when Woods made his Middle East debut. Coverage of the event dominated world newspapers and television bulletins for a week, thrusting Dubai into the global consciousness. Woods' appearance in Dubai will have been worth millions of dollars to the emirate in promotion.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/golf1.jpg" align="left" hspace="5" vspace="5" width="200" height="179"> Looking at the barren brown golf courses Oman currently has to offer - where players carry a patch of astro turf around what is effectively an 18-hole bunker - plans for an international tournament featuring the best players in the world might sound like a dream.&nbsp;<br>
                    <br>
                    But it is a dream Oman is determined to turn into a reality.&nbsp;<br>
                    The Sultanate is poised to get its first green golf course, with construction due to begin behind the Seeb Novotel in October this year.<br>
                    To be named the Muscat Golf and Country Club, the unique 18-hole tournament-grade course has been fashioned by top designer David Thomas to challenge the best golfers in the world and should be open for business by October 2003.&nbsp;<br>
                    Using the rugged terrain in Seeb to dramatic effect, its grass fairways, lush greens, trees and lakes will contrast with the brown wadis and hills of the area. On higher ground around its periphery, 100 villas will look down on the spectacular course and out to the Seeb coastline.<br>
                    The project includes a state-of-the-art clubhouse with restaurant, swimming pools, tennis and squash courts and recreational areas. There will also be a driving range, and a 9-hole practice course for beginners.<br>
                    The golf course is closely linked to the soon-to-proceed $US600 million Muscat Waste Water project, which will supply the 3000 cubic metres of water a day needed to transform the barren desert site into a green grass oasis.&nbsp;</font></p>
                    <p><img border="0" src="../../images/2001_may_june/golf2.jpg" align="left" hspace="5" vspace="5" width="199" height="277"><font face="Verdana" size="2">In an exclusive interview, the man behind the RO19 million venture, His Highness Sayyid Kais bin Tarik Al Said, spoke to OER of his vision for a world-class golf course that will not only lift the Sultanate's profile and boost its tourism industry, but encourage Omanis to take up the game he loves.<br>
                    "Golf is becoming a very popular sport worldwide," he says.&nbsp;<br>
                    "It has become popular because it is no longer now a sport that only the very well-off play. Now a much wider range of people play the game, and it is much more affordable and accessible. And it is an interesting and attractive activity, at least that's what I think. It's spreading even in Oman now. We have quite a few Omanis who play the game, but on sand of course."<br>
                    Sayyid Kais says the introduction of a green course will be accompanied by a drive to get young Omanis into golf which, he hopes, will one day see them competing at the top level.&nbsp;<br>
                    "Once there is a green course we will concentrate on the Omani juniors and youth players, have a programme to introduce it at that level, so that we can have hopefully one day Omanis playing as professionals," he says.<br>
                    "The younger you are the better you are, like Tiger Woods, who is only 25."&nbsp;</font></p>
                    <p><font face="Verdana" size="2">But it took more than 10 years for Sayyid Kais, together with his brother HH Sayyid Talal bin Tarik Al Said, to bring his idea to the brink of fruition.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/golf3.jpg" align="right" hspace="5" vspace="5" width="224" height="187">A 1990 proposal to build a green course on another site in Muscat near Bandar Jissah was shelved due to constraints on the land available. And a 1995 feasibility study by KPMG concluded that a green course would not be economically viable due to the high water requirement.<br>
                    However, two significant events in the past 12 months breathed new life into the dream.&nbsp;<br>
                    In May last year, His Majesty Sultan Qaboos bin Said granted the land behind Seeb Novotel for the site. Sayyid Kais says His Majesty's generosity and foresight were crucial for the project to proceed.<br>
                    The site's proximity to Muscat's population and infrastructure makes it ideal for a golf course, he says. "It is also a very rugged and a typically Omani landscape, which will differentiate it from the golf courses that are being built at this time around the Gulf area. These other courses are usually shaped by bulldozers and do not really reflect the landscape of the country."<br>
                    The second key event occurred just four months ago, when Sayyid Kais personally approached the Minister of Commerce and Industry, His Excellency Maqbool bin Ali Sultan, and negotiated an allocation of water for the course from the Muscat Waste Water project.<br>
                    "Fortunately both the Muscat Municipality and the Minister of Commerce have agreed to supply this water free of charge to the project. So that was a great hurdle to overcome," he says.<br>
                    "Lately there is a great push to increase tourism in Oman, and one of the things that the Ministry of Commerce and Industry and the Department of Tourism want to see is green golf courses, after seeing how they have benefited Dubai."<br>
                    But a golf course alone would not have been profitable enough to make the venture attractive. So it was decided to put in some 100 villas, which will generate rentals of between RO800 to RO1,200 each per month.<br>
                    "So suddenly, it has become a very viable project," says Sayyid Kais.&nbsp;<br>
                    "We are looking at around a 15 per cent return on investment."<br>
                    He is in talks with ANZ Bank and Oman International Bank to provide finance for the project.<br>
                    <br>
                    The course has been designed to stand out among the best courses in the world, and the prospect of Oman holding an international tournament with the best players in the world is very real.<br>
                    "Our course will not be of any less quality than the ones they play on anywhere in the world," His Highness says.&nbsp;<br>
                    "Eventually we want to attract the best players, and at the moment Tiger Woods is the best in the world. He is incredible.<br>
                    "We have already had requests from the person who organises the Dubai Desert Classic to look into possibly bringing a tournament here to Oman, and whether it will be included in, say, the European Tour, which the Desert Classic is.<br>
                    "From Dubai the players travel to Qatar, which has also become part of the European Tour. While the weather is bad in Europe, they could have maybe a three-week swing through the Gulf. But this of course will all depend how popular golf will become here in Oman."<br>
                    He believes it will take two to three years from its opening in 2003 for the course to mature enough for an international tournament to be held, making 2005/06 a possible target.&nbsp;<br>
                    Such a tournament would undoubtedly lift Oman's international profile and provide a huge fillip for the tourism industry.<br>
                    "You get this televised by satellite, like in the case of Dubai, and it's a very good promotion for the country," says Sayyid Kais.<br>
                    "With just a swing of the camera you can get an idea about the country."<br>
                    <br>
                    Emirates Golf Club - the first club in the Middle East to have an all-green course, and host to the recent Dubai Desert Classic - confirms that grass courses have helped to transform Dubai's tourism industry.&nbsp;<br>
                    "The introduction of grass golf courses to Dubai was an integral facet in the promotion of tourism both in the UAE and in the region itself," says Chris Turlik, membership services manager at Emirates Golf Club.<br>
                    "Golf has been part of a huge tourism drive in Dubai over the past 10 years, and Dubai now offers four 18-hole grass courses that are of world-class quality."<br>
                    The Dubai Desert Classic, a European Tour Event, has been played 12 times on the Majlis Course at Emirates Golf Club and at The Dubai Creek Golf &amp; Yacht Club.<br>
                    Turlik says Emirates Golf Club hosted a staggering 60,000 rounds of golf last year alone, "which is a clear indication of the immense popularity of Dubai as the Golfing Capital of The Middle East".&nbsp;<br>
                    <br>
 Not surprisingly, hotels in Muscat are brimming with excitement at the prospect of a green golf course.&nbsp;<br>
                    Folkert Van Dyk, director of business development at the Grand Hyatt Muscat, says the course will attract a new type of business clientele to the Sultanate and will "definitely put Muscat on the world map".<br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/golf4.jpg" align="left" hspace="5" vspace="5" width="171" height="240">"You'd automatically start targeting a completely new type of business clientele," he says. "I lived in Saudi Arabia, and a large number of our clients at the Hyatt there would very frequently go to Dubai for a weekend of golf. So that's one group within the GCC that you could definitely market to. And then of course you have the golf tour operators within Europe."<br>
                    Brown golf courses, while different and unique, simply don't have the same pulling power as a green course, Van Dyk says.&nbsp;<br>
                    <br>
                    "It's not something that you would put in a brochure either: 'Located three minutes from a brown golf course...' No."<br>
                    Hakim Magid Saud Al Maameri, tourism manager for Zubair Tourism and coordinator of the massive $200 million Barr Al Jissah Resort project, says the construction of a grass golf course is a significant step for Oman, allowing it to tap into the multi-billion dollar golf tourism market.<br>
                    "The golf market as far as holidays go is worth $44 billion a year worldwide," he says. "So that's a huge market. Although it's untested here, we do think it will do very well. We see golf courses as an important element for tourism. It's a selling point for the hotels. It will really be a boost for tourism."<br>
                    He says Zubair would have looked at building a green golf course in Oman if somebody else hadn't beaten them to it.<br>
                    "We would like to look into setting up a golf course which could be associated with hotels, but since someone's going ahead already, let's see how that one takes off."&nbsp;<br>
                    Marko Hytonen, general manager of Muscat's Radisson SAS Hotel, says it is important for Oman to offer golf as a tourist activity. "Radisson SAS is very involved with golf," he says. "We sponsor a lot of golf activities, and to have a green course here would definitely be an additional attraction for the destination."&nbsp;<br>
                    Kaare Johansen, regional director of marketing for Bass Hotels and Resorts which represents the Al Bustan, Inter-Continental and Crowne Plaza hotels in Muscat, says attracting sporting superstars like Woods to Oman would be a major coup.<br>
                    "He (Woods) is one of the biggest names in world sports today," he says.&nbsp;<br>
                    "That Dubai was able to get Tiger Woods, of course for an undisclosed amount, has done enormous things for their promotion. For an entire week it was 'how is Tiger Woods doing in Dubai?'"&nbsp;<br>
                    <br>
                    Woods lost the Desert Classic, coming second to Dane Thomas Bjorn. Of course, he then went on to win his second US Masters, becoming the only man in history to hold all four major golfing titles at once.<br>
                    But the point is: even if Woods were to miss a crucial putt on the 18th hole and lose the first Oman Arabian Open, Oman would already have won.</font></p>
