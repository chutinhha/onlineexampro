                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Big deal</font><p><font face="Verdana" size="1"><b>The merger between Bank Dhofar and Majan International Bank is firming up as the management of both the banks awaits the shareholders' approval over the proposed takeover offer. OER reports</b></font><br>
                    <font face="Verdana" size="2">
                    <br>
                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2002_august/DHOFAR-BANK.jpg" align="left" hspace="5" vspace="5"></font><font size="2" face="Verdana">I</font><font size="2" face="Verdana">t's not official yet. But the stage is all set for the much-hyped merger between Bank Dhofar Al Omani Al Fransi and Majan International Bank (MIB). Last fortnight, the MIB board met and is understood to have given a go-ahead to the deal, which reportedly involves a swap ratio of 2.35 MIB shares for a single Bank Dhofar share. The merger between both the banks has been talked about for the past several months and, according to the industry sources, the agreed ratio (1:2.35) is the best offer MIB can get.<br>
                    <br>
                    But there is still a bit of tension among the banks, as the deal has to be finally approved by the shareholders. When contacted, MIB's General Manager Salaam Al Shaksy could only say, "We are awaiting the final nod from the Extraordinary General Body Meeting (EGM) of the shareholders, which will be held either in August or September. I cannot speak beyond this until the meeting happens."&nbsp;<br>
                    <br>
                    OER tried reaching the top management of Bank Dhofar, but none were available for comment.&nbsp;<br>
                    <br>
                    The deal also has to get the necessary approvals from the Capital Market Authority (CMA) and Ministry of Commerce and Industry (MOCI). Subsequently, the Bank Dhofar board will seek its shareholders' approval.<br>
                    <br>
                    The purchase consideration is expected to be a combination of shares, cash and bonds. Accordingly, 50 per cent of MIB shares would be converted into Bank Dhofar shares. A 25 per cent of the purchase consideration will be paid as cash at par and the balance 25 per cent as five-year bonds at a coupon rate of six per cent.<br>
                    <br>
                    Before the deal was agreed upon, quite a few offers and counteroffers have been made between the two sides. Bank Dhofar reportedly offered a ratio of 2.5 MIB Bank shares for each of its share. However, MIB made a counter offer of 2.25 MIB shares for every Bank Dhofar share and a bond coupon rate of seven per cent. But the ice was broken when Bank Dhofar made a final offer of a swap ratio of 1:2.35 and six per cent interest rate for the five-year bonds.<br>
                    <br>
                    Bank Dhofar's post merger share capital will go up to RO40 million. As for MIB, merger is one of the ways to salvage its fortunes as it had registered a loss of RO7.3 million for the year FY01 against a net profit of RO1.9 million in FY00. The total loan loss provisions for MIB had bulged by over 40 times to RO9.2 million as compared to a mere RO228,000 in the previous year. So it's a win-win situation for the banks.<br>
                    <br>
                    On the other hand, Bank Dhofar is the only bank in the Sultanate that had posted a growth in net profit of RO6.13 million in 2001 as compared to RO5.53 million the previous year. Moreover, for the first half of the year 2001, Bank Dhofar had reported a net profit of RO3.73 million.<br>
                    <br>
                    The merged entity will have 49 branches, while a few of them may be closed down at locations where both the banks already have branches. The integration of operations and resources would lead to sustained cost savings for the merged entity. As far as the management is concerned, it's not yet clear how the top brass of both the banks will be accommodated within the new bank.&nbsp;</font></p>
