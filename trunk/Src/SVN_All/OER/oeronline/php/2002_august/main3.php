                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Switch your cash online&nbsp;</font><p><font face="Verdana" size="1"><b>Convenient banking is passè. Now it's convenient remittance. Bahwan Cybertec's online remittance service - CashSwitch - hopes to cash in on the huge funds flowing out of the Gulf region, reports Sahad
                    P.V.</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><img border="0" src="../../images/2002_august/cashswitch1.jpg" align="left" hspace="5" vspace="5" width="225" height="164"><font face="Verdana" size="2">Abraham Koshy, an executive working with a reputed private company in Muscat, performs a monthly ritual. Soon after his salary gets credited to his bank account, Koshy drives down to the nearest bank branch, which is about 15 minutes away. He then stands in a queue for 10 minutes and withdraws some cash and rushes to a foreign exchange dealer to transfer the money to his folks living in India. Koshy does this every month and one thing he hates is going to the bank and the forex dealer, which takes away a cool 45 minutes from his busy working day. And he is just one of the seven million expatriates living in the GCC countries performing this ritual almost every month.&nbsp;<br>
                    <br>
                    But the inconveniences of the executives like Koshy or for that matter anybody who wishes to transfer money, has turned out to be a lucrative business opportunity. Dubai-based CashSwitch Corp., promoted by Bahwan Cybertek (BCT), recently launched the region's first e-remittance service in Dubai, which will make transferring funds easier. Presently, from the United Arab Emirates, you can transfer funds to India, Pakistan, Sri Lanka, Bangladesh and the Philippines. Though the product was formally launched in Oman in July, customers will get to use the service only in the last quarter of the year.&nbsp;<br>
                    <br>
                    How does CashSwitch make your life easier? At first you need to log on to
                    <a href="http://www.cashswitch.com"> www.cashswitch.com</a>, which has partnered with banks and exchange houses. Select the bank where you have an account and then allocate the amount for transfer. Then select the exchange house that gives you the best exchange rate and complete the remittance. The money will be delivered to your beneficiary or credited to your bank account back home. The entire activity would take you not more than a couple of minutes, but the convenience comes at a minor cost. Currently, CashSwitch charges a flat rate of 25 Dirhams as commission in the UAE for any amount of transfer.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_august/durga.jpg" align="left" hspace="5" vspace="5" width="134" height="175"> In the UAE, the company has tied up with two banks - Emirates Bank International and Middle East Bank - and one exchange house, Thomas Cook Al Rostamani. In Oman, BCT has initiated talks with all leading banks and the exchange houses to start its e-remittance service. Says a senior BankMuscat official, "BCT has approached us, but we are evaluating the benefits."&nbsp;<br>
                    <br>
                    CashSwitch expects to rope in at least three banks in Oman to begin with. Says S Durgaprasad, Director and CEO, BCT, "Our aim is to reduce the prevailing inconvenience to the customers, and promote a new approach." For BCT, CashSwitch is an opportunity to take a big leap forward and become an IT major in the Middle East. In fact, the only other company that provides online remittance services in the world is Western Union, which offers the services between the United States and Canada.&nbsp;<br>
                    <br>
                    BCT hopes to cash in on the big opportunity in fund transfer business of the region. Durgaprasad says, "Any business is driven by opportunities. We saw a huge potential for online fund transfers as the current physical remittance systems are creating inconveniences to the people."<br>
                    <br>
                    According to recent survey published in Dubai-based Gulf News, expatriate workers in the GCC transferred about US$107 billion over the past five years, with Saudi Arabia accounting for nearly two-thirds of the transfers.&nbsp;<br>
                    <br>
                    Annual remittances from Saudi Arabia averaged around US$15 billion during the five-year period while the UAE came second, with an average US$3 billion. Average outflows in Kuwait stood at around US$2 billion, while they ranged between US$300-US$900 million a year in the remaining GCC members - Qatar, Bahrain and Oman. Indians and Pakistanis were the biggest remitters, accounting for nearly one third of the total annual transfers.&nbsp;<br>
                    <br>
                    CashSwitch intends to grab at least five to six per cent of 1.5 million transactions per month happening in the UAE and Oman. Since its launch in Dubai on June 15, the company has done more than 300 fund transfers. As per Durgaprasad, the biggest challenge for him will be to draw a crowd on to the Internet for transactions, which is still in its infancy in this part of the world.</font></p>
