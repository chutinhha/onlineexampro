                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Hard sell, easy instalment</font><p><font face="Verdana" size="1"><b>The auto finance industry is a dominant player in the Sultanate, which has benefited both the automotive sector and consumers in a big way. While auto dealers have been able to lift their businesses greatly, the cars are within the close reach of consumers. OER takes a look at this vital industry.</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><font face="Verdana" size="2">If we are to attribute a single factor to the proliferation of cars all over the Sultanate, it is the easy instalment schemes offered by auto finance companies. Let's face it. If we want to buy a car today, most of us may not even bother about the brand. Instead, we simply browse through the newspapers and check for the screaming advertisements which may read - Only RO50 a month or Only RO25 a month, etc. And the company offering the best deal comes out trumps.<br>
                    <br>
                    While attractive schemes also display the competitiveness within the automobile industry, more often these are observed as the necessary tools devised to help the local population to satisfy its basic need of transportation. In fact, it is the country's underdeveloped public transport system that is responsible for making a large percentage of the population dependent on personal vehicles rather than buying them for luxury. As a result, the future of the automobile industry is now directly linked to the increasing youth population, which is the prime target segment for most of the dealers. Naturally, considering the overall dynamics of the economy, low-paying entry level jobs plus the ever-present need, will force the dealers to team up with auto finance companies to launch attractive instalment schemes and special offers.<br>
                    <br>
                    <b>Increasing awareness</b><br>
                    It is estimated that every year 28,000 to 30,000 cars are sold in Oman. Out of these 60 to 65 per cent are financed, which means around 18,000 cars are financed each year. Explaining the trend, Aftab Patel, CEO, Al Omaniya Financial Services, observes, "The consumers have become aware of the convenience. They have realised that they do not have to block their money to purchase a car, and keep some for emergencies."<br>
                    <br>
                    Speaking on the interest rate scenario, Patel argues that securing auto finance through purely financial companies proves much cheaper than taking personal loans from banks. "The interest rates in most of the banks range between 12 to 13 per cent, whereas with finance companies, the interest rate is as low as 10 to 11 per cent. Apart from that, you also retain the flexibility of keeping money in hand," he says.<br>
                    <br>
                    There is no easy financing. Most of the car dealers have tie-ups with finance companies, whereby finance representatives are present at showrooms. One can close a deal very quickly, it is a matter of simply filling in a few forms. But the gruelling test begins when the financing companies begin their questionnaire. For their own protection, finance companies look at the bank statements of the prospective client and check for loans with other financiers.<br>
                    <br>
                    <b>Salary-driven enterprise</b><br>
                    Another trend that is clearly visible in the Omani market is that most of the individual loans are salary-driven. By doing this a finance company ensures that its asset is completely protected. Pankaj Parekh of National Finance Company says, "We make sure that the instalment/net salary ratio of an individual does not exceed 35 per cent. The remaining 65 per cent gives him the room to move around. It may be extended to 40 per cent, but then the down payment gets bigger."<br>
                    <br>
                    "The client should have at least 50 per cent of his or her salary as minimum take home," points out Sohraab Hasnain, Manager at the Zubair's Azaiba showroom."<br>
                    <br>
                    If the company finances a vehicle bought by someone, the registration is done in the joint names of the buyer and the finance company," Parekh adds. The registration is renewed every year until the instalments are paid. The release letter is then written on the sole name of the buyer.<br>
                    <br>
                    <b>The evolution</b><br>
                    A cursory look at the auto finance scene may make people think it has always been around. But, auto financing is a fairly new phenomenon in Oman, notes Aftab Patel. "We saw it emerging in Oman in an organised form in the late 1980s, to be precise 1987 and 1988. Prior to that auto finance was not in an institutional form (as it is now). Only the dealers were doing it for seven-months and so on. From 1988 onwards there was a renewed awareness. And since then the sector has come a long way. "Before people used to hesitate even to fill forms or even sign their names. They used to get upset when they were enquired about their salaries," says Patel.<br>
                    <br>
                    As far as the market is concerned, most of the dealers agree that the prime car sales season is during Ramadhan, when some of them rake in more money in a month than during the rest of the year. The best schemes, offers and deals roll out of the woodwork then. Car companies vie with each other to sell. A major din in the market emerges and auto finance companies work overtime, checking and double-checking bank statements and salaries of prospective customers."<br>
                    <br>
                    Essentially, there are two types of markets - replacement and new. The replacement market consists of buyers who have already had cars but want another one. New market represents the first time buyers. The replacement market holds the bigger chunk, covering 80 per cent of the market. The first time buyers will eventually become subsequent buyers and will become a part of the replacement market," says Patel.&nbsp;<br>
                    <br>
                    Hasnain says, "Around 95 per cent of the deals concluded are spread over a period of five years. The annual interest rate is 6.85 per cent. That should make a cumulative interest rate of 34.25 per cent at the end of five years, with the consumer paying an extra RO1600 to RO1800 depending on the car value."<br>
                    <br>
                    <b>The problems</b><br>
                    However, like any other sector, the auto finance industry also has problems. "The general problem is non-payment - be it due to bouncing checks, loss of job, unanticipated payments, over commitment, sickness, etc. Circumstances such as sickness can be resolved in the short term, but problems such as over commitment have to be changed," Patel feels.<br>
                    <br>
                    The auto financing schemes are here to stay, as long as the Omani population is growing. However, more interesting is the fact that the Sultanate represents the most competitive market in the entire Gulf region, where cars are extremely cheap. This means that dealers are going to bombard the local market with all sorts of offers to lure the customers.&nbsp;</font></p>
