                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    Lessons From Change – Oil &amp; Gas Industry</font></b><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2"><br>
				Oil and gas companies are reassessing their operating practices 
				and capital allocation and have understood the need to maintain 
				capital expenditure on projects that will deliver growth<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				In the last article, we looked across a broad analysis and 
				established a broader agenda for action that would be relevant 
				for all businesses — in every sector, in every country. As a 
				consequence, we believe that a new performance agenda is 
				emerging as companies respond to the downturn and prepare for a 
				different future. In this article, we will look at the Lessons 
				From Change for the oil and gas industry.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				Managing the cycle<br>
				</span></font>The oil and gas sector is accustomed to commodity 
				price fluctuations and the need to respond quickly to changing 
				market conditions. However, the speed and severity of the 
				current downturn took many by surprise and the degree of 
				uncertainty about the level of future demand is probably at its 
				highest in decades.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				Unevenly affected<br>
				</span></font>The oil and gas industry features a wide range of 
				participants and different subsectors of the industry have been 
				unevenly affected by the economic downturn. When oil prices were 
				above $100 per barrel and demand was outstripping supply, the 
				top priority for most companies was maintaining and increasing 
				production, with cost control taking a secondary role. A year 
				later, the new market reality is forcing many oil and gas 
				companies to reassess their operating practices, investments and 
				capital allocation. The need to maintain capital expenditure on 
				projects that will deliver long-term growth is understood by all 
				players.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				capital and debt<br>
				</span></font>Reduced availability of capital and tougher credit 
				terms will impact the ability of junior companies to fund 
				ongoing operations and bring assets into production. They are 
				prioritising their most advanced or promising projects to 
				minimise investment risk and increase revenue streams.<br>
				Many of the smaller oilfield services companies have also found 
				it difficult to raise funds. They have seen a drop in demand for 
				their services and have also come under intense pressure from 
				customers to reduce costs. Consequently, they are responding to 
				the lower margin outlook and reduced activity levels by reducing 
				headcount, deferring new equipment orders and being more 
				selective in bidding for projects.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">new 
				challenges<br>
				</span></font>The oil and gas industry is sophisticated and 
				mature in terms of its approach to both risk management and 
				governance. Most companies are already engaged in reviewing the 
				effectiveness of their procedures on a regular and ongoing basis 
				and have used existing risk management and governance mechanisms 
				to good effect during the course of the downturn. <br>
				<br>
				Managing influences<br>
				Much of the world’s remaining oil and gas reserves are located 
				in countries perceived to be riskier due to the lack of 
				stability in their political, regulatory and fiscal regimes. As 
				a result, oil and gas companies are well versed in preparing 
				for, and proactively responding to, external challenges, 
				particularly the risk of new regulatory and compliance mandates.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">cost 
				reduction<br>
				</span></font>With global demand falling for both oil and gas 
				over the past year, they are focusing on trying to improve the 
				bottom line through cost-cutting with areas of particular focus 
				being headcount reduction, working capital management and 
				supplier costs. Companies have scrutinised their relationships 
				with customers and suppliers more closely over the past 12 
				months without jeopardising the commercial viability of their 
				key suppliers to deliver on their commitments and keep work 
				programmes on track.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				organisational structure<br>
				</span></font>The headcount reductions announced by some oil 
				companies are not purely about removing people from the payroll. 
				In a number of cases, it is part of a wider strategy to simplify 
				the organisation, to speed up the decision-making process and 
				get the right people in the right types of jobs. The downturn is 
				prompting companies to accelerate the rollout of transformation 
				programmes that have identifiable cost savings.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				Waiting for the upturn<br>
				</span></font>To date the sector has not seen a significant 
				increase in transaction activity as considerable uncertainty 
				still exists around the shape, speed and sustainability of 
				economic recovery. Distressed assets abound but the oil majors 
				are likely to proceed cautiously with any acquisitions. In the 
				downstream sector, the focus for the oil majors has continued to 
				revolve around the disposal of assets in noncore markets and 
				differential investment in markets with superior growth 
				potential. The financial crisis has constrained the ability of 
				private equity and independent players to fund further 
				acquisitions.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">
				Alliances increasing<br>
				</span></font>The economic slowdown, lower oil prices and the 
				new economic environment have made the benefits of partnership 
				more compelling for some companies and governments are becoming 
				increasingly active in encouraging and brokering alliances in 
				the oil and gas sphere. The downturn is also likely to increase 
				the appetite for joint ventures as they provide the opportunity 
				for companies to share the operational and financial risks of 
				so-called megaprojects.<br>
				<br>
				<font color="#C00000">
				<span style="font-weight: 700; background-color: #FFFF00">The 
				road to recovery<br>
				</span></font>According to our survey, respondents from 
				international oil and gas companies are slightly more optimistic 
				about industry prospects than respondents from other industries, 
				and envision an outlook for increased strategic acquisitions and 
				alliances over the next 12 months. However, there is 
				considerable uncertainty around the likely timing, shape and 
				sustainability of any recovery. Any recovery is widely predicted 
				to be slow and uneven.<br>
				<br>
				In the long term, the overall structural issues surrounding 
				location of reserves and achievable levels of production have 
				not changed. Oil and gas companies must not allow today’s 
				difficult operating environment to prevent them from addressing 
				the need to replace reserves, nor from preparing themselves for 
				the time when growth resumes. The industry has a long and 
				successful history of coping with volatile prices and difficult 
				operating environments; that experience will serve companies 
				well in coping with the challenges that lie ahead.</font><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				Executives who show ingenuity, have the courage to make tough 
				decisions and demonstrate the foresight to apply lessons from 
				change will guide their companies to success in the oil and gas 
				sector. And they will be the leaders who establish the 
				foundation upon which the new global economy will rise. </font><div align="center">
