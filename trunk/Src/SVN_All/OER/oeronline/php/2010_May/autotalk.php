			Spur and the Continental Supersports. This was more than enough to 
			give us an impression of what to expect from the other cars.<br>
			<br>
			The Flying Spur is a handsome and good-looking vehicle by any 
			standard but doesn’t stand out as much as the price suggests. Inside 
			and out the vehicle, at first glance, looks as plush and classy as 
			most hi-end luxury vehicles but it is when you look a little closer 
			that it truly comes into its own. It’s restrained looks wonderfully 
			mask its most striking features – the beautifully handcrafted 
			interior and immaculate attention to detail. Every inch of the 
			vehicle feels like it was specially built to please you and if you 
			order one it definitely is. This is because Bentley is more than 
			willing to customise the vehicle exactly to your liking, even 
			including custom fittings and furnishings if you should so desire 
			(with just a few exceptions), making nearly every vehicle unique.<br>
			<br>
			If you are the sort of person that is looking for a top-class luxury 
			vehicle but would rather drive himself as opposed to being 
			chauffeured around, then the Flying Spur, or maybe even one of the 
			other models, is definitely the car for you. Power is available in 
			abundance with 552hp and 650Nm of torque at your disposal from the 
			twin-turbo W12, the latter of which is available from as low as 
			1600rpm. And if you feel that this is not enough, there is always 
			the Flying Spur Speed with 600hp on tap and an additional 100Nm of 
			torque to give you the extra edge you desire. The vehicles all-wheel 
			drive system helps get this power to the road effectively and 
			cleanly and, working with the adjustable suspensions, it tunes the 
			vehicle behaviour exactly to your liking. Aside from the expected 
			sky-high price and running costs, and the fact that we can’t afford 
			one yet, there is little else to complain about. <br>
			<br>
			The Continental Supersports on the other hand is an entirely 
			different beast. One look at this vehicle and you just know you are 
			in for something special. The vehicle began as an under the radar 
			project to explore the possibilities of reducing weight to realise 
			the full potential of the regular GT’s formidable W12 power unit. 
			The result was astounding to say the least. The cars weight was 
			reduced by around 110kg and it was reengineered, retuned and 
			remapped to achieve phenomenal supercar potency. Power was raised to 
			621hp while torque was raised to 800Nm, the result – 100kmph is 
			cracked in just 3.9 seconds and it never lets go till it hits max 
			velocity somewhere north of 320kmph.<br>
			<br>
			From the first time you sit behind the wheel and plant you foot on 
			the gas pedal, its sheer power and torque demands immediate respect. 
			The grip and agility levels of the ‘regular’ Bentley models are good 
			especially considering their weight, but the Supersports is in a 
			whole different league. In no other model in the family will you see 
			the true potential of the W12 power plant. Hit the first corner and 
			you will realise just how much more a ‘Driver’s car’ the Supersports 
			is. It corners are flatter and harder than any other Bentley and 
			with max torque available nearly throughout the rev-range picking up 
			speed is never an issue. But one key aspect to keep in mind at all 
			times is – although lighter and more nimble than its GT counterpart, 
			it is still a rather heavy vehicle and it is only once you take this 
			into account can you truly appreciate the full potential of the 
			Supersports.
