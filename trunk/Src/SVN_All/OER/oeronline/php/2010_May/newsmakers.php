					<b><font face="Verdana" size="2">Majan consolidated is 
					planning to set up a college of international standards for 
					media studies</font></b></p>
				<p align="center" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/may-newsmakers01.jpg" hspace="9" vspace="5" style="border: 4px solid #C0C0C0; " width="505" height="291"></font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><b>Oman is likely to become 
					the preferred destination of those who aspire to go for 
					media studies of international standards. A university 
					college for media studies of global standards owned by Majan 
					Consolidated is expected to come up in the Wilayat of Seeb 
					in the Sultanate next year.<br>
					</b>
					<br>
					The college named University College of Media Arts and 
					Sciences will have in its faculty and governing body some of 
					the globally known names in journalism and entertainment 
					industry, like the popular TV producer from the US, Gary L 
					Pudney. It is likely to attract students from abroad also 
					thereby reversing the current trend of students going abroad 
					from Oman 
					for studies.<b><br>
					<br>
					</b>Pudney, who is the president of Gary L Pudney Company, a 
					production company, is acclaimed as the highest ranking 
					talent executive in the history of broadcasting. His TV 
					projects and specials have won over 50 Emmy awards and he is 
					known to be close to celebrities, royalty and other 
					dignitaries.<br>
					<br>
					Pudney and Majan Consolidated are also planning to organise 
					media related events like a ‘Global Cultural Award 
					Programme’ in the Sultanate which is expected to attract 
					international media, celebrities and high end tourists to 
					the Sultanate.<br>
					<br>
					HH Sayyed Nawaf Barghash Said al Said vice chairman of Majan 
					Consolidated told OER that Pudney will be the chairman of 
					advisory board and Professor Gary Allen English, a well 
					known name in media studies, will be the CEO of the college.<br>
					<br>
					HH Al Said mentioned that the idea of establishing a 
					university was the brainchild of Professor Gary English who 
					visited the Sultanate a few months back. “More importantly, 
					His Majesty Sultan Qaboos bin Said’s vision encompasses 
					media and arts and he loves them and supports them. It also 
					inspired me to start the college,” he noted. He also 
					informed that two globally renowned universities have 
					evinced interest in having strategic partnership with the 
					media college.<br>
					<br>
					“Strategic partnership means a high level of cooperation and 
					the university will be completely owned by Majan 
					Consolidated,” said group CEO of Majan Consolidated Anvwar 
					Hussain Al Balushi. <br>
					<br>
					On the courses planned, English and Al Balushi explained 
					that the curriculum was in the development stage. 
					“Broadcasting will be a major,” Al Balushi said. “Several TV 
					networks have requested for such a course. We want to 
					discuss with media companies in the region on whether they 
					want us to start a minor in print journalism,” noted 
					English.<br>
					<br>
					The Majan Consolidated officials along with Gary L Pudney 
					paid a visit to HE Salem bin Nasser Al Maskari, 
					secretary–general of the Council for Higher Education and 
					discussed about the college. HE Al Maskari welcomed the 
					idea, said Al Balushi.<br>
					<br>
					On the mega cultural event, Pudney said it will be a unique 
					programme to honour great personalities for the 
					contributions made in their career. The event will bring a 
					new concept of honours. The event will include performances 
					and renowned hosts and will be broadcasted live globally by 
					the international media. </font></p>
