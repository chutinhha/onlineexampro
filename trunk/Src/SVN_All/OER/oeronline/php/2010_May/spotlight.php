                    </font><b><font face="Verdana" size="4" color="#C00000">How 
				it began</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
                </font><b><font face="Verdana" size="2">A comprehensive guide on 
				air-conditioners including the history, design and style trends 
				developed to help you endure the sunshine</font><font face="Verdana" size="1"><br>
                <br>
                </font></b><font face="Verdana" size="1"><b>The History</b><br>
				The Middle East isn’t exactly known for its pleasant summers. 
				But thankfull we can more than cope with the heat thanks to an, 
				often overlooked, invention called the air conditioner. But how 
				did they come into existence and who was the true inventor of 
				the air conditioner? Read on to find out. First and foremost, 
				before we go into the business breakdown of air conditioners, 
				let us explain how it all began and how the modern air con came 
				into existence.</font><p align="center" style="margin: 3px 10px" dir="ltr">
				<img border="0" src="images/may-ac.jpg" width="505" height="276" hspace="7" vspace="7"><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">While air conditioning itself is a relatively new invention, the 
				cooling of buildings is not. It is known that wealthy ancient 
				Romans used aqueducts to circulate water through walls to cool 
				their luxurious houses. Even as far back as the 2nd century 
				China there have been records of some form of air conditioning. 
				It is recorded that inventor Ding Huan, of the Han Dynasty, 
				invented a rotary fan for air conditioning, with seven wheels 3m 
				in diameter and was manually powered. In 747, Emperor Xuanzong 
				of the Tang Dynasty had a Cool Hall, called Liang Tian, built in 
				the imperial palace and was said to have water-powered fan 
				wheels for air conditioning as well as a rising jet stream of 
				water from fountains. During the subsequent Song Dynasty, 
				written records mentioned the widespread use of rotary fan as a 
				means of air conditioning.<br>
				<br>
				Even medieval Persia was said to have some sort of cooling 
				system for buildings during hot weather seasons. They used 
				cisterns, which were large open pools in the central courtyards 
				used to collect rain water, in conjunction with impressive wind 
				towers. These wind towers had windows to catch the wind which 
				was then directed, using internal vanes, down into the building, 
				over the cistern and out through a downwind cooling tower. As 
				the cistern water evaporated it cooled the air inside the 
				building. In 1200AD, Abd al-Latif al-Baghda described the use of 
				ventilators in various households in Egypt. He reported that 
				almost every house in Cairo has a ventilator, and that they cost 
				anywhere from 1 to 500 dinars depending on their sizes and 
				shapes. In fact it is said that Ventilators were actually 
				invented in medieval Egypt.<br>
				<br>
				But it was only in 1820 that British scientist and inventor 
				Michael Faraday discovered that compressing and liquefying 
				ammonia could chill air when the liquefied ammonia was allowed 
				to evaporate. In 1842, a Florida physician John Gorrie used 
				compressor technology to create ice, which he used to cool air 
				for his patients in his hospital. He hoped eventually to use his 
				ice-making machine to regulate the temperature of buildings and 
				even envisioned centralised air conditioning that could cool 
				entire cities. Although his prototype leaked and performed 
				irregularly, Gorrie was granted a patent in 1851 for his 
				ice-making machine. Unfortunately, any hopes for its success 
				soon vanished with the death of his chief financial backer; 
				without the money he needed there was no way to develop the 
				machine commercially. According to his biographer, Vivian M. 
				Sherlock, he blamed the “Ice King”, Frederic Tudor, for his 
				failure, suspecting that Tudor had launched a smear campaign 
				against his invention. Dr. Gorrie died impoverished in 1855 and 
				the idea of air conditioning faded with him for the next 50 
				years.<br>
				<br>
				In 1902 the first modern electrical air conditioning was 
				invented by Willis Haviland Carrier. Designed to improve 
				manufacturing process control in a printing plant, his invention 
				controlled not only temperature but also humidity. The low heat 
				and humidity were to help maintain consistent paper dimensions 
				and ink alignment. Later Carrier’s technology was applied to 
				increase productivity in the workplace. This led to the 
				formation of The Carrier Air Conditioning Company of America to 
				meet rising demand. In 1906 Stuart W. Cramer was exploring ways 
				to add moisture to the air in his textile mill. Cramer coined 
				the term “air conditioning”, using it in a patent claim he filed 
				that year. He combined moisture with the ventilation to 
				“condition” (This evaporation of water in air, to provide a 
				cooling effect, is now known as evaporative cooling) and change 
				the air in the factories, controlling the humidity so necessary 
				in textile plants. Willis Carrier adopted the term “air 
				conditioning” and incorporated it into the name of his company. 
				Early commercial applications of air conditioning were primarily 
				to provide cool air for industrial processing rather than 
				personal comfort but over time air conditioning came to be used 
				to improve comfort in homes and later in automobiles. In fact it 
				wasn’t until the 1950s that Residential sales expanded 
				dramatically.<br>
				<br>
				The first air conditioners and refrigerators employed toxic or 
				flammable gases like ammonia, methyl chloride, and propane which 
				could result in fatal accidents when they leaked. Thomas 
				Midgley, Jr. created the first chlorofluorocarbon gas, Freon, in 
				1928. The refrigerant was much safer for humans but was later 
				found to be harmful to the atmosphere which resulted biological 
				consequences including an increase in skin cancer, damage to 
				plants, and the reduction of ocean plankton.<br>
				<br>
				Freon is a trademark name of DuPont (an American chemical 
				company) for any Chlorofluorocarbon (CFC), Hydrogenated CFC 
				(HCFC), or Hydrofluorocarbon (HFC) refrigerant. The blend most 
				used in direct-expansion home and building comfort cooling is an 
				HCFC known as R-22. It is to be phased out for use in new 
				equipment by 2010 and completely discontinued by 2020. Several 
				non-ozone depleting refrigerants have been developed as 
				alternatives, including R-410A, invented by Honeywell in Buffalo 
				NY and sold under the Genetron AZ-20 name.<br>
				<br>
				Innovation in air conditioning technologies continue, with much 
				recent emphasis placed on energy efficiency and improving indoor 
				air quality. As an alternative to conventional refrigerants, 
				natural alternatives like CO2 (R-744) have been proposed. We 
				only have to wait and see what comes of it.<br>
				<br>
				<b>The Current Business of Commercial Cooling<br>
				</b>While globally the air-conditioning market is growing by 
				leaps and bounds, in countries such as the Middle East the 
				segment has witnessed enormous growth. In the GCC for instance, 
				massive construction drives spurring the development of numerous 
				residential and commercial real estate projects and high 
				population growth have fuelled demand for air-conditioners and 
				cooling systems.<br>
				<br>
				The boom in real estate development together with the growing 
				emphasis on greater cooling facilities and the resultant 
				increase in power demand have all contributed to creating huge 
				opportunities for manufactures and distributors in district 
				cooling. This trend has received added impetus in recent years 
				with the lack of sufficient generation, transmission and 
				distribution infrastructure to meet projected power demands.<br>
				<br>
				The Middle East has been a major market for air-conditioners, 
				coolants and refrigerators, chiefly due to the region’s arid 
				climatic conditions. However, in recent times, other factors 
				have been reinforcing the market’s growth in the segment. These 
				include improvements in lifestyle, the mushrooming of numerous 
				malls and hypermarkets all over the region and other 
				developmental activities, all of which has spurred demand for 
				air-conditioning products.<br>
				<br>
				‘Air conditioners have come a long way from being just a cooling 
				machine. Today we have models that can detect and eliminate 
				deadly disease causing agents like SARS and bird-flu virus and 
				other harmful pathogens this is done through a technology called 
				MPI [Micro Plasma Ion], a world’s first from SAMSUNG,’ says Ajay 
				Ganti, CEO, SARCO distributors for SAMSUNG in Oman.<br>
				<br>
				He goes on to add that newer models of air-conditioners 
				“incorporate what is called GOOD SLEEP MODE, which senses your 
				body temperature to regulate the room temperature. There is a 
				lot of focus on hygiene and health. There is also a lot of 
				impetus on the design accent to turn the air-conditioner into a 
				piece of art, to make it a part of your interior decor. So 
				basically, the AC is a lifestyle product rather than just a 
				cooling product”.<br>
				<br>
				In Oman, the air-conditioner and refrigerator market account for 
				a whopping 65 to 70 per cent of all business in the white goods 
				market. “The market size of air-conditioning products is around 
				200 K units a year in Oman. The market size for Refrigerators is 
				around 80 K units a year. Also due to a very fertile commercial 
				/ industrial projects scenario, there is also a great business 
				opportunity for different air-conditioning concepts like 
				chiller, ducted type or DVM(digital Variable Multi),” says 
				Ganti. Traditionally, the Sultanate has been a market for window 
				ACs. That trend is rapidly changing as improved standards of 
				living and more sophisticated lifestyles push customer 
				preference to split and ducted air-conditioners. This is in line 
				with global trends. Apart from lifestyle changes and economic 
				development, price reductions in split units have driven demand 
				for these models of air-conditioners. What’s more, the 
				air-conditioner which was earlier regarded as a seasonal product 
				now boasts decent sales figures even during off-season. In Oman 
				extreme temperatures and high levels of humidity have spurred 
				the demand for high quality, energy efficient, environment 
				friendly and reliable cooling solutions. The Omani government, 
				in keeping with these trends and demands, has established the 
				highest standards in setting requirements for all new projects 
				being developed for both the tourism and real estate sectors. In 
				order to meet these requirements and to reduce the demand on the 
				governments for energy the District Cooling solution has been 
				developed and implemented throughout the GCC.<br>
