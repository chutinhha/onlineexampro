						<td valign="top" colspan="3">
						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">Toyota Motor Corporation 
						announced its remedy for the problem concerning the 
						Vehicle Stability Control (VSC) programme in its Lexus 
						GX 460 and certain Land Cruiser Prado vehicles. In 
						circumstances in which advanced driving skills are 
						required, such as when a driver of the involved vehicle 
						sharply turns the steering wheel in high-speed 
						conditions, or when the driver negotiates a curve at 
						excessive speeds, the vehicle could slide sideways, due 
						to the insufficient activation of the VSC.<br>
						<br>
						<b>The following conditions combine to cause the 
						problem:</b><br>
						In the GX 460 and Land Cruiser Prado, heavy components, 
						such as the fuel tank, are located on the left side, and 
						in left-hand drive versions, the left side is made even 
						heavier because of the presence of the driver.<br>
						<br>
						For vehicles with certain tires (on 18-inch wheels) and 
						equipped with the Kinetic Dynamic Suspension System (KDSS) 
						and VSC, the activation of the VSC may not be sufficient 
						in certain circumstances due to the setting of the VSC 
						programme.<br>
						<br>
						Circumstances may require advanced driving skills, such 
						as sharp turns of the steering wheel in high-speed 
						conditions or negotiating a curve to the right at 
						excessive speeds.<br>
						<br>
						As a remedy, Toyota will update the VSC programme to 
						enhance the effectiveness of the VSC so that the risk of 
						the vehicle sliding, even to the point that it is almost 
						sideways, will be reduced.<br>
						<br>
						<b>Vehicles involved are as follows:</b><br>
						GX 460: Approximately 13,000 units in total. 
						Approximately 9,400 units in the US, approximately 1,000 
						units in Russia, approximately 1,000 units in Oman.<br>
						<br>
						Land Cruiser Prado: Approximately. 21,000 units in total 
						(only left-hand drive vehicles with KDSS and VSC). 
						Approximately 4,400 units in Oman, approximately 4,000 
						units in Russia, approximately 1,500 units in the UAE. 
						No right-hand drive units have this problem.<br>
						<br>
						In total, approximately 34,000 units worldwide are 
						involved. In North America, Toyota Motor Sales, U.S.A., 
						Inc. (TMS) and Toyota Canada Inc. (TCI) each announced a 
						voluntary recall on April 19. As for other regions and 
						countries involved, such as in Europe and the Middle 
						East, TMC will work to implement similar measures as 
						soon as possible.</font></td>
