				<font face="Verdana" size="1"><b>
				<span style="background-color: #FFFF00">OGWA 2010</span></b>, 
				the three-day oil and gas exhibition and conference saw an 
				overwhelming response from guests and speakers, making it one of 
				the largest oil and gas exhibitions in the region. Organised by 
				Omanexpo, the event was formally inaugurated by HE Nasser bin 
				Khamis Al Jashmi, undersecretary of the Ministry of Oil and Gas, 
				in the presence of VVIPs and other dignitaries, ministry 
				officials, ambassadors, the top management of participating 
				companies, and other distinguished guests.<br>
				<br>
				Dr Zaid Khamis Al Siyabi, chairman of the conference and 
				director general of exploration and production, Ministry of Oil 
				and Gas, informed that there were more than 250 companies 
				showcasing their products and know-how. If there was more more 
				space another 250 companies could have been added. “On the 
				conference side we had 400 delegates representing 92 companies 
				from 51 countries. We received around 250 papers for the 
				conference, out of which we could accommodate only 60 papers,” 
				Dr Siyabi added.<br>
				<br>
				OGWA 2010 featured the national oil and gas companies from the 
				GCC, international oil and gas majors, leading local companies, 
				service providers, equipment suppliers and other companies 
				directly serving the industry’s growing requirements. Held under 
				the patronage of Oman’s Ministry of Oil and Gas, the 2010 event 
				was the biggest ever and set records in terms of the number of 
				participating companies, countries represented, the total 
				exhibition space covered (over 4,000 square metres), and number 
				of visitors (8,232).<br>
				<br>
				<b>Bigger and better<br>
				<br>
				</b>The accompanying SPE EOR Conference at OGWA, which was 
				organised by the Society of Petroleum Engineers, was also the 
				biggest ever conference in the Middle East that focused on 
				enhanced oil recovery (EOR) methods and technologies. Held at 
				the Golden Tulip Hotel, right next to the Oman International 
				Exhibition Centre, the conference attracted hundreds of 
				delegates from all over the world and featured 14 technical 
				sessions under the theme “EOR Challenges, Experiences and 
				Opportunities in the Middle East.”<br>
				<br>
				“OGWA 2010 prominently highlighted the continued growth of the 
				vibrant oil and gas industry in Oman and the wider region, 
				providing industry professionals the opportunity to inspect the 
				equipment and technologies required in exploration and 
				production activities while discussing new developments that can 
				shape the future of the industry,” said Nasser Diab, general 
				manager of Omanexpo. <br>
				<br>
				He stressed that the strong local and international 
				participation in OGWA 2010 clearly reflected the massive 
				potential and opportunities in Oman’s oil and gas industry and 
				it also indicated the vibrancy of the regional market as all 
				countries in the Middle East continues to boost their upstream, 
				midstream and downstream operations. Diab added that by getting 
				together industry professionals from within the region and other 
				parts of the world, OGWA 2010 effectively served as a vital 
				forum for assessing the current state of the industry and 
				charting its course for the future.<br>
				<br>
				The media sponsors of OGWA 2010 included Sabco Media as the 
				official media company, Pipeline Magazine as the official 
				publication, Al Wisal FM as the official radio station, and 
				Zawya as the business community partner. The media partners were 
