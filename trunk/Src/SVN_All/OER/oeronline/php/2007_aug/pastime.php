</font><b><font face="Verdana" size="4">Zest for life</font></b><font face="Verdana" size="1"><br>
<b>For Sandeep Mishra, chief operating officer, Shanfari Group of Companies, 
planning to the last detail helps enjoy life to the fullest. Rekha Baala meets 
up with him</b><br>
<br>
<img border="0" src="pass.jpg" width="250" height="184" align="left" hspace="6" vspace="4">Calm, cool and collected – that is Sandeep Mishra, chief operating officer, 
Shanfari Group of Companies. As we meet him an hour after office hours, he is 
catching up on some routine work, checking e-mails and winding up things for the 
day. He gives no sign of any tension or pressure that one normally associates 
with a high-profile job like his. Minutes into the interview and it is clear 
that the man has an attitude that surely helps to keep stress at a long 
distance. Sandeep is back in Oman on a second stint; his previous one lasted 
three-and-a-half years. He gave up a chance to be in happening Dubai to return 
to a place he describes as ‘addictive’ and which, most importantly, fuels his 
inherent and constant passion for travel and adventure. “I have been to most 
places in Oman during my earlier stint. We generally used to take off on 
weekends on short trips. With pristine beaches and majestic mountains, Oman 
offers an irresistible combination rarely found in other parts of the world.”<br>
<br>
<b>Oman is perfect</b><br>
A thorough ‘outdoor’ person, Sandeep says the best way to get out of work is to 
have your little ‘time-outs’. “My visits to Bahala, Nizwa and Barka have been 
absolutely delightful. I still remember my first trip to Jebel Akdhar vividly – 
the temperature was around five degrees. It was quite an experience. The best 
part about Oman is that there are so many places to see in and around Muscat. 
There is no need to travel long distances all the time.” This passion to get 
behind the wheels of the best cars makes Sandeep’s adventurous forays much more 
exciting. “I love driving good cars and driving in Oman offers me great 
pleasure. I am into it for total adventure… not the car racing sort.”<br>
<br>
But it’s not just in Oman where Sandeep has been bitten by the travelling and 
adventure bug. It has been a passion helped by his constant moves to various 
countries on work. “I have lived in different cities like Houston, Tokyo, New 
Delhi and Calcutta and travelled to many others. But Muscat tops my list as the 
perfect place to be with family.” <br>
<br>
Sandeep is waiting for his family to join him in Muscat so that his jaunts begin 
all over again. Meanwhile, he is catching up on reading and watching television. 
While books on management and economics are his favourites, he also likes 
fiction. Currently, he is reading the voluminous A Suitable Boy by Vikram Seth. 
The time alone is also an opportunity to indulge in another favourite pastime – 
eating out.<br>
<br>
<b>Points to ponder<br>
</b>Though he likes to try out different kinds of food, he professes a fondness 
for Mughlai cuisine. This brings us to the all-important question: his thoughts 
on a healthy lifestyle. He confesses to that currently he is like ‘an arm-chair 
philosopher’ but he believes, without doubt, that one needs to follow a healthy 
lifestyle. “I miss the lifestyle of my earlier days when I used to play 
different sports, especially racquet games. I would definitely like to get into 
any kind of sport right away.”<br>
<br>
But career demands do keep one busy. In his career spanning 21 years in leading 
companies, Sandeep has worked directly with the top management. “If you have a 
proper structure, everything is well-planned and strategies are in place, then 
there is no room for stress. I am quite used to a professional routine which has 
given me the strength and experience to handle different situations.” Planning 
is important, so is time management if you want to keep stress at bay, insists 
this corporate achiever whose tips to beat stress are worth following. “Plan 
your job well, set your goals, be loyal to your profession and be true to 
yourself. Also, how you spend your time away from work is as important as your 
work itself. It is imperative that you strike the right balance between the 
two,” he says. </font>
