</font><b><font face="Verdana" size="2" color="#CC3300">BankMuscat H1 net up 
43.6% to RO40.2 mn</font></b><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">
<img border="0" src="bus2.jpg" width="150" height="179" align="right" hspace="6"></font></b><font face="Verdana" size="1">BankMuscat has reported a net profit of RO40.2 million for the first half of the 
year ended June 30, 2007, as against a net profit of RO28 million reported 
during the corresponding period in 2006, thus recording a 43.6-per cent growth. 
Net interest income increased by 28.9 per cent to RO59.3 million during the 
first half of 2007 from RO46 million reported during the corresponding period a 
year ago, mainly driven by asset growth. Non-interest income has grown by 39.5 
per cent to RO20.8 million for the first half of 2007, compared to RO14.9 
million reported during the same period in 2006.<br>
<br>
Operating expenses increased by 28.3 per cent during the period mainly due to 
increase in manpower and other administrative costs as a result of increased 
business activities and expansion of business lines. However, cost to income 
ratio reduced to 39.5 per cent during the first half of 2007 as against 40.6 per 
cent during the first half of 2006. Impairment for credit losses was RO7.7 
million for the first half of 2007 as compared to RO8.1 million during the 
corresponding period in 2006.</font><hr color="#C0C0C0" width="80%" size="1">
