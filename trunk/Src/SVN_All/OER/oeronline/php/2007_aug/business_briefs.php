<font face="Verdana" size="2" color="#CC3300"><br>
PDO celebrates 40th anniversary</font></b><font face="Verdana" size="1"><br>
</font><b>
<font face="Verdana" size="2" color="#CC3300">
<img border="0" src="bus5.jpg" width="300" height="177" align="right" hspace="6" vspace="4"></font></b><font face="Verdana" size="1">On July 27, 1967, the oil tanker Mosprince arrived at Mina al Fahal (or Saih al 
Maleh, as it was then known) to load the first export cargo of Omani crude. Over 
the 40 years that have elapsed since that particular date, PDO has exported over 
7.5 billion barrels of crude oil for the benefit of the Sultanate. To celebrate 
its 40th anniversary, PDO recently organised an event that attracted the who’s 
who of Oman. On the occasion, PDO released the special book Underground to 
Overseas: The Story of Petroleum Development Oman, written by Sir Terence Clark, 
the former British ambassador to Oman. The book, launched by HE Nasser bin 
Khamis Al Jashmi, Undersecretary, Ministry of Oil and Gas, chronicles PDO’s long 
journey from producing oil from just a couple fields in 1967 to producing oil, 
gas and condensates from over 100 fields today. Underground to Overseas traces 
PDO’s history with help of interviews of over 200 past and present members of 
staff. “This is a book not only about the past, it is also about the present 
with a glimpse into the future,” said Sir Terence. “It shows through the 
accounts of individual Omanis how, with the support and encouragement of PDO 
management, what was originally a rather exclusive expatriate company has been 
transformed into an integrated member of the wider Omani community.”<br>
<br>
“Our aim in staging this book launch as well as other 40th-anniversary 
celebrations is threefold,” said Abdul-Amir al Ajmi, PDO’s External Affairs and 
Communications Manager. “The first is simply to mark the event which was 
critical to the Sultanate’s development; the second is to pay tribute to PDO 
staff – past and present – who have made our continued oil and gas production 
possible; and, finally, it’s a good opportunity to communicate to the Omani 
public PDO’s ongoing commitment to the Sultanate’s renaissance.”<br>
<br>
Addressing the distinguished audience, PDO’s Managing Director John Malcolm 
said: “More than forty years ago, Omani Government entrusted us with the 
challenging task of developing an oil industry in Oman. Since 1970, we have been 
blessed with His Majesty’s unwavering support as well as the support of His 
Government, most notably the Ministry of Oil and Gas with which we work in close 
partnership.”</font><hr color="#C0C0C0" width="80%" size="1">
<p align="justify" style="margin: 3px 10px"><b>
<font face="Verdana" size="2" color="#CC3300"><br>
Omantel exploring Pakistani market</font></b><font face="Verdana" size="1"><br>
According to a report released by Omantel on the occasion of Renaissance Day, 
the company is currently studying the purchase of a majority stake in World Call 
Telecom Company based in Pakistan. Omantel has started the negotiations with the 
company’s shareholders.<br>
<br>
The report further said that Omantel had signed RO7-million agreement with 
Huwaei for the provision of multi-purpose broadband for ADSL Internet service 
covering all regions of the Sultanate. This agreement will facilitate the 
rollout over 100-180 thousand new ADSL lines. To promote digital culture, 
Omantel has introduced wireless Internet service known as WiFi in more than 20 
locations in commercial complexes and cafes, to enable Internet surfing on 
laptops and mobile phones by using prepaid cards. On the fixed line front, 
Omantel has introduced wireless fixed line system known as the Wireless Local 
Loop (WLL) to cover mountainous and rural areas where it was difficult to extend 
fixed telephone cables. WLL services now covers more than 160 rural villages in 
various governorates and regions in the Sultanate. Omantel will also prepare the 
telecommunications infrastructure for Sohar Port. The company is working towards 
concluding agreements with the concerned authorities to establish the port 
management system. Omantel is currently conducting feasibility studies for 
several international projects regarding submarine cables including SMW-V 
project, which is the fifth generation of SMW.<br>
<br>
Omantel has achieved 93 per cent Omanisation by the end of June this year. This 
is one of the highest localisation percentages among telecom companies not only 
in the Sultanate but also among other Gulf states.</font><hr color="#C0C0C0" width="80%" size="1">
