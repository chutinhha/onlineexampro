Aviation Services, the company’s management has been chalking out plans for new 
aircraft, new destinations, new routes and other modernisation initiatives. Oman 
Air recently reconstituted its board. Ziad Bin Karim Al Haremi, Chief Executive, 
Oman Air, said, “On July 3, 2007, a new board assumed the responsibilities of 
elevating our company to new heights. The composition of the new board of 
directors is as follows: HE Ahmed bin Abdulnabi Macki, Minister of National 
Economy &amp; Deputy Chairman of Financial Affairs &amp; Energy Resources Council 
