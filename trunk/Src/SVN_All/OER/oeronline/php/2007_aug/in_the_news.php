      <p align="justify"><font face="Verdana" size="1"><b>Global concern for biodiversity<br>
      </b><br>
Worldwide, oil and gas exploration and production companies consider 
biodiversity and environment to be a key element of their corporate 
responsibility. Take Shell, for instance. It has adopted a biodiversity standard 
outlining its commitment to work with others to maintain ecosystems, respect 
protected areas and seek partnerships to conserve biodiversity. In 2003, it 
adopted a policy with regard to protected areas, including a commitment not to 
explore for, or develop oil and gas resources in natural World Heritage Sites. 
In 2005, it also developed a biodiversity strategy through to 2010, which 
includes a commitment to follow strict operating practices in protected areas 
classified under the World Conservation Union (IUCN) Category System as Category 
I-IV protected areas and in other areas of high biodiversity value. </font></td>
