<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Major setback to nature’s preservation</font></b><font face="Verdana" size="1"><br>
<b>Unesco has removed Oman’s Arabian Oryx Sanctuary from the World Heritage List. 
Is it the urge to earn more petro dollars or preserve nature more effectively? 
OER finds out about the underlying factors behind the development</b><br>
<br>
<img border="0" src="news.jpg" width="253" height="189" align="left" hspace="6" vspace="4">In a major blow to Oman’s efforts to conserve nature, the World Heritage 
Committee on June 28 decided to remove the Arabian Oryx Sanctuary from Unesco’s 
World Heritage List. It was the first instance when any world heritage site was 
deleted since Unesco’s 1972 Convention concerning the ‘Protection of the World 
Cultural and Natural Heritage’ came into force. In fact, when it was listed in 
the Unesco list, it was first site from the region to get such recognition. The 
Arabian Oryx Project was initiated in 1979 by the order of HM Sultan Qaboos bin 
Said, with the aim of re-establishing the wild population of the Arabian Oryx, 
which had become extinct. As part of the initiative, the Arabian Oryx Sanctuary 
was established in 1994 to protect these animals and conserve the desert habitat 
as well as other threatened species.<br>
<br>
So, what led to the removal of the sanctuary from Unesco’s list? Unesco states, 
“The World Heritage Committee deleted the property because of Oman’s decision to 
reduce the size of the protected area by 90 per cent in contravention of the 
guidelines of the convention. This was seen by the committee as destroying the 
universal value of the site. In 1996, the population of the Arabian Oryx in the 
site was 450 but it has since dwindled to 65, with only about four breeding 
pairs making its future viability uncertain. This decline is due to poaching and 
habitat degradation. After extensive consultation, the committee felt that the 
unilateral reduction in the size of the sanctuary and plans to proceed with 
hydrocarbon prospects will destroy the value and integrity of the property.”<br>
<br>
The sanctuary originally covered approximately 27,500 sq km of central Oman. Now 
it occupies just 3,800 sq km. But it’s not the Unesco which decided to remove it 
from the coveted list. Rather, it was the government of Oman that asked Unesco 
to do so. As Ali Amer Al Kiyumi, Director General, Nature Conservation, Ministry 
of Regional Municipalities, Environment and Water Resources says, “It is the 
Government of Oman that asked the Unesco to remove the sanctuary from the list 
for different reasons. The first was that the sanctuary is a huge area with a 
rough topography, comprising wadis, mountains, desert, etc., and that made it 
very difficult for us to effectively maintain surveillance. In the absence of a 
proper control, there was widespread poaching of Oryx that caused a decline in 
their numbers. The poachers, coming from outside Oman, used to trap live Oryx 
and sell them to private farms. We tried by all means to control the collapse. 
We used our manpower, ROP, the military, you name it, but there is a limit to 
the manpower you can engage in to protect the sanctuary. This is because the 
more manpower you deploy in a protected area, the more damages you’ll do to the 
habitat. Therefore, in order to conserve the ecosystem of Oryx, gazelle and 
other wildlife of the sanctuary, we decided to reduce its size so that we could 
manage the smaller sanctuary area more effectively.” But there is more to the 
story. Kiyumi says, “Long before the sanctuary was set up, a large part of the 
area had been given to oil and gas companies as concession areas. And as per the 
contract, these companies had already been exploring oil in the area. However, 
the oil exploration activities are not going to affect the local environ in a 
significant way. Oil companies having activities in the area comply with 
environmental regulations. They must have an environment permit and there are 
guidelines on how to work within protected areas.” The change in the status of 
the sanctuary means that the doors have been opened for exploration. Kiyumi 
further says: “the Government of Oman has also taken various conservation 
measures to protect the animals. Though the Oryx numbers were declining but now 
it’s fixed. Apart from 65 Oryx in the sanctuary, we have put around 140 Oryx in 
captivity so that they can multiply. And when we’ll find ourselves capable of 
controlling poaching, we’ll release them in the wild or move them to other 
protected areas.”<br>
<br>
