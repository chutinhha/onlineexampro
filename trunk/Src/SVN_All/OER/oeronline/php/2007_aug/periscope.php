<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The Singapore Bull Run</font></b><font face="Verdana" size="1"><br>
<b>The Singapore success story is all about exports and FDI, but it is domestic 
spending that will be the catalyst for GDP growth in the next five years, writes 
Matein Khalid</b></font><b><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1">
<img border="0" src="peri1.jpg" width="148" height="239" align="left" hspace="6" vspace="4">The Straits Times Index in Singapore has soared almost 50 per cent since last 
summer’s emerging market selloff. In the process, Singapore has become the 
highest beta market in Southeast Asia. Sure, short term corrections are 
inevitable as the fallout from the US sub-prime mortgage meltdown raises 
tangible fears of real estate deflation, with all its attendant impact on US 
economic growth and consumer demand for Asian imports. Alan Greenspan even 
spooked the financial markets with his prediction of a one-in-three chance of 
recession in 2008. Nigerian violence, US refining bottlenecks, the peak US 
summer driving season and a tight spot market has pushed North Sea crude oil to 
US$75. US Treasury bond yields have surged from 4.5 per cent to 5.2 per cent in 
the past three months. Meanwhile, the Chinese stock market bubble has become a 
policy dilemma for the People’s Bank and the Beijing politburo, who are 
horrified by the speculative animal spirits of 100 million share traders in the 
Middle Kingdom. A US recession, an oil shock, a surge in Uncle Sam’s long term 
debt and collateral damage from a Shanghai A share crash can easily slash 10-15 
per cent from the Strait Times Index in the next three months. After all, the 
index has soared from 3000 to 3600 since March. The summer is also seasonally 
the weakest and most volatile period for Singapore shares.<br>
<br>
While the Straits Times Index is overbought at 3600, Singapore is a classic “buy 
on dips” stock market. The island state exhibits extraordinary economic growth 
potential, as it reengineers its economy as finance, tourism, media and 
technology hub of Southeast Asia. Exports, consumption, massive government 
investment, a construction boom have all contributed to accelerate its GDP 
growth to 6.5 per cent. This means that earnings growth in Singapore can easily 
top 17-20 per cent in both 2007 and 2008. The property rally in Singapore has 
also broadened from luxury condos and premium office space to fringe and 
suburban areas, meaning that the island state is the most compelling asset 
reflation story in Southeast Asia, even as the government positions it to become 
a private banking and wealth management centre for the Pacific Basin. The 
Singapore dollar, managed by one of the world’s most well respected monetary 
agencies, has emerged as a safe haven, hard money currency, an Asian Swissie 
that attracts flight capital from ASEAN and China, while dampening domestic 
inflation. Singapore financial markets also exhibit exceptional liquidity, as 
evidenced by the softness in money market rates (SIBOR), capital inflows and 
huge IPO oversubscription, similar to the GCC.<br>
<br>
<b>Inflated valuations</b><br>
Of course, the secular bull run in Singapore since 2004 has led to inflated 
valuation metrics. The Straits Times is expensive at 17 times current earnings 
and 2.5 times price to book value, at least on standard deviation above its 
long-term value. Singapore is, after all, a highly cyclical market with its 
property, oil and gas, technology and banking shares.<br>
<br>
Yet Singapore’s valuation can stay inflated as long as earnings growth 
momentum/bullish property prices continue and there are no traumatic 
international financial shocks (e.g., Wall Street credit crunch, war in the 
Gulf, risk aversion in emerging markets or a rise in US treasury bond yields to 
six per cent). Moreover, Singapore equities are also boosted by an embryonic M&amp;A 
wave, particularly after the listed technology firm Amtek was subject to a 
takeover bid by a consortium led by Standard Chartered Private Equity and CVC 
Capital Partners, and KKR’s takeover of hard disk maker MMI Holdings. 
Singapore’s blue chips have a regional dimension. Keppel Corp is a proxy for the 
booming offshore oil and gas, luxury real estate and Asian infrastructure 
sectors. Keppel operates offshore oil drilling rigs for the world’s largest oil 
producing companies. Its Keppel Land subsidiary benefits from Marina bay 
Residences. With 30 per cent earnings growth, Keppel is a true growth stock on 
the SGX.<br>
<br>
While the Singapore success story under Lee Kwan Yew was all about exports and 
FDI, domestic spending would be the catalyst for the 7-8 per cent annual GDP 
growth in the next five years. Entire new sunrise industries, from wealth 
management to biotechnology to two integrated resorts software/media will mean 
more than a million new jobs and high end immigration from China, India and even 
the West. The Singapore government is targeting a population of 6.5 million by 
2020. The unquestionable beneficiary of Singapore’s consumer spending property 
boom will be banking shares, whose loans and deposits books correlate with 
accelerating GDP growth. Singapore banks still trade at or below two 2 times 
book value OCBC is the play on mass property market reflation and mortgage 
finance, while DBS is an emerging Asian trophy bank. Singapore is home to some 
of the best-managed REITs in Asia. High rental values and sales process mean 
REITs, with a robust pipeline of acquisition opportunities, can significantly 
increase NAV. Capitaland is the most diversified Singapore property bluechip but 
office, logistics and resort REITs also merit attention. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
