Branson, Khosla invest 
in biofuel firm<br>
</font></b><font face="Verdana" size="1">Entrepreneurial luminaries Vinod Khosla 
and Richard Branson have made significant investments in a company that is 
working on the development and production of advanced biofuels. Khosla, managing 
partner of Khosla Ventures and co-founder of Sun Microsystems, and Branson, 
founder of Virgin Fuels and other components of a business empire organised 
under the Virgin brand, are giving their backing to Gevo Inc., a Pasadena-based 
company seeking to transform biomass into butanol fuel on an industrial scale. 
The retention of Khosla Ventures and Virgin Fuels will provide the small 
start-up firm with substantial financial and managerial backing. Branson has 
said that he hopes his fleet of commercial and private planes will one day run 
on biofuel. </font><hr color="#C0C0C0" width="90%" size="1">
