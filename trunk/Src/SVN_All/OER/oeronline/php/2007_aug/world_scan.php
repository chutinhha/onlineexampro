<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">Online video ads on hot 
track</font></b><font face="Verdana" size="1"><br>
As per a study by eMarketer, online video advertising is expected to boom by 
2011 when the lines between TV and Web video will be blurred. Video ad sales are 
expected to grow from an estimated US$775 million this year to US$3.1 billion in 
2010 and then to $4.3 billion in 2011. The advertisers will begin directing more 
of their marketing budgets to the online version. By 2011, online commercials 
will likely appear in multiple forms beyond today’s pre-roll ads, which users 
must sit through before watching a video clip. For example, some interactive 
banners will play an ad whenever a user clicks it or rolls the cursor over it. 
And graphics along the bottom or side of a video clip will encourage users to 
watch a commercial. It is also expected that more online video ads will offer 
rewards in exchange for the user’s time. Potential payoffs may include free 
content, games, coupons, or ways for users to personalise commercials. Companies 
are already experimenting with the new video ad formats. </font><hr color="#C0C0C0" width="90%" size="1">
