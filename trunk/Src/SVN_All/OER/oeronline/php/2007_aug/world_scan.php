Reportedly, PepsiCo made an initial approach to Nestle. But Nestle was concerned 
that a deal with Pepsi would dilute its mission of selling more healthy food and 
beverages. There were also complications of structuring a deal. Pepsi, in 
addition to its colas, owns the Frito-Lay brand of potato chips. Nestle also 
