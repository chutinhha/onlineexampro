</font><b><font face="Verdana" size="4">The bulls prevail</font></b><font face="Verdana" size="1"><br>
<b>The uptrend witnessed in the first half of the year is expected to continue 
for a while in the MSM due to strong local and overseas liquidity</b><br>
<br>
The Muscat Securities Market (MSM) continues to rule bullish despite verdicts of 
correction by many doomsayers. The benchmark MSM30 Index gained 107 points or 
1.7 per cent during the period, propped up mainly by the banking and industrial 
indices. However, market activity, as measured by total turnover, declined by 
almost 31 per cent to RO107.4 million, mainly due to lower institutional 
participation. Select counters witnessed increased volumes on anticipated 
positive news flow. In due course, ONIC Holding, Oman Flour Mills, Al Jazeera 
Steel, Al Maha Petroleum and Oman Cables announced encouraging results.<br>
<br>
In key corporate events, the management of Alliance Housing Bank has urged the 
Board to offer a bonus issue post its private placement of a 35 per cent stake 
with Ahli United Bank of Bahrain. Though the stock witnessed some sudden gain on 
the announcement, it could not sustain the enthusiasm.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="market1.jpg" width="450" height="419"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<br>
In another development, Al Jazeera Steel announced that a strategic investment 
proposal has been received from Global Investment House, Kuwait, for taking a 51 
per cent equity stake in the company by way of additional share issue, at 325 
baiza per share. As per the company, a number of other potential investors have 
also shown interest in picking up a similar equity stake and it is in 
discussions with them. <br>
<br>
MSM modified its MSM30 Index constituents from July 1, 2007. This annual 
modification is in line with the MSM30 Index maintenance rules. As before, the 
capping factor has been reset to 20 per cent as the maximum weight for any 
company in the index, as well as in its sub-indices. Alliance Housing Bank, Al 
Anwar Holding, Oman Investment &amp; Finance (OIFC), United Power Co., Dhofar Cattle 
Feed, National Aluminum Products, Al Jazeera Steel Products Co. have been added 
to the Index. Dhofar International Development &amp; Investment Co. (DIDIC), Oman, 
Emirates Investment, Transgulf Investment Holding, Dhofar Power, Oman Textile 
Mills Holding, Majan Glass and Taghleef Industries Co. have been removed from 
the index.<br>
<br>
The uptrend witnessed in the first half of the year is expected to continue for 
a while in the MSM due to strong local and overseas liquidity. Given the huge 
growth potential, valuations have been expanding for key index stocks, which 
have been driving MSM30 to new heights. <br>
<br>
<b>GCC MARKET COMMENTARY</b><br>
The GCC markets ended on a positive note for the four-week period ended July 15. 
Saudi market (Tadawul), which had seen heavy losses in recent times recovered 
sharply, becoming the top performing market in the region, generating a return 
of 8.77 per cent. The Qatari market was the second biggest gainer for the 
period, generating a return of 7.01 per cent. The Kuwaiti and the Bahraini 
bourses registered a gain of 4.58 per cent and 4.16 per cent, respectively. The 
ADSM and DFM were slow movers and registered a growth of 1.94 per cent and 1.27 
per cent, respectively. The Abu Dhabi market occupies an ace position among 
other bourses, with year to date return of 22.59 per cent followed by Kuwait 
with 22.11 per cent. <br>
