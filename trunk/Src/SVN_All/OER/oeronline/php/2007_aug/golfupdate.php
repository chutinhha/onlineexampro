<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Making the ball dance to his tune<br>
</font></b><font face="Verdana" size="1">
<b>A profile of Seve Ballesteros, who has retired at the age of 50 after a 
record-breaking career<br>
</b><br>
<img border="0" src="golf.jpg" width="200" height="226" align="left" hspace="6" vspace="4">It was a Tour career that began at the 1974 Spanish Open with him scoring 83 and 
ended 23 years later with him 16 over par and joint last with a 67-year-old Lee 
Trevino. But in between what joy and what excitement Seve Ballesteros brought to 
the world of golf. The European game had certainly never seen anything like him 
and it was not just that he was brilliant with a short game to die for. He was 
charisma personified and every emotion was worn on his sleeve for all to see. 
Five majors – three Opens, two Masters – a record 50 European Tour victories, 
six World Match Plays, 31 other wins around the world and, of course, all those 
Ryder Cup highlights. <br>
<br>
The match could have been made for him. It was indeed changed to accommodate him 
because the year he won his first Open at Royal Lytham in 1979 was also the year 
that continental Europeans were allowed to play. Every time he played and then 
as captain in 1997, he poured his heart and soul into beating the Americans – 
and how he enjoyed it when Europe did. With Jose Maria Olazabal, he formed the 
deadliest partnership in the event’s history, winning 11 and halving two of 
their 15 games together.<br>
<br>
Ballesteros first announced his arrival as a world star at the age of 19, 
finishing runner-up to Johnny Miller in the Open at Birkdale after leading the 
first three days. The chip-and-run between bunkers to tie with Jack Nicklaus on 
the final hole is still recalled to this day and it was only another three years 
before he became the youngest winner of the title for almost 100 years. No 
European had won the Masters at that stage, but he soon changed that, dominating 
two of the next four and, come the 1984 Open at St Andrews, he was the man to 
deny Tom Watson a record-equalling sixth win. Another win at Lytham in 1988, 
achieved with a superlative closing 65 to beat Nick Price, proved to be his 
final major, but there were lots more highlights to come.<br>
<br>
He continued playing in the Ryder Cup until 1995 and, even though he had become 
a wild hitter by then, he guided David Gilford to a victory in the fourballs 
before showing Tom Lehman that despite hitting it all over the place in their 
singles clash, he was still capable of magic. With a chronic back condition 
everybody knew they had seen the best of him, but he was determined to battle on 
despite falling further and further down the world rankings. As Ryder Cup 
captain at Valderrama – it was taken to Spain for the first time largely because 
of him – he used to wake vice-captain Miguel Angel Jimenez up in the early hours 
to tell him his latest plan.<br>
<br>
Not every player liked the way he went about things, more hands-all-over than 
hands-on, but he got the result he wanted. Long into the decline that has now 
finally brought his retirement, he even beat Montgomerie twice in the duel 
between the captains at the Seve Trophy, but when that result was reversed at El 
Saler in 2003, Ballesteros was not seen on a course again for nearly two years. 
But it was not the end. When he came back at the Madrid Open, he still 
entertained hopes then of finding some form, but that it did not happen and now, 
years after people first started calling for him to stop playing, he has.<br>
<br>
The current generation might find it hard to imagine the impact he had. But try 
this – if Ballesteros was in Britain and in contention with Nick Faldo, Sandy 
Lyle, Ian Woosnam or any other British golfer, he was the one most people wanted 
to win. <i>(Source: Sportinglife) </i> </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
