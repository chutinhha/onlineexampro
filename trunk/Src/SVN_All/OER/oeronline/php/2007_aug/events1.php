Speaker of the year in 2003 and 2004. With a strong background in holistic 
development and in the health and fitness arena, Carol has extensive coaching 
experience and has often been heard promoting the virtues of a healthy lifestyle 
on the local radio station. She holds a Diploma in Yoga and teaching 
certificates from the American Council of Exercise (ACE).<br>
