<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Stepping into the uncertain<br>
</font></b><font face="Verdana" size="1"><b>Training and development processes 
are undergoing immense changes as the profile of the employee changes with time, 
writes Paul Bridle</b><br>
<br>
<img border="0" src="manage2.jpg" width="152" height="359" align="left" hspace="6" vspace="4">Many people believe that training and development is all about courses, training 
days, workshops, or attending a university or a business school. Training is 
something done for them or to them. Many see it as achieving a qualification or 
a certificate.<br>
<br>
Traditionally, this might have been the case. However, training and development 
are changing, both in terms of the methods of delivery and in terms of who 
‘owns’ the process. This is affecting the way that training is delivered as well 
as how people are approaching their development. There is no single reason why 
the changes are happening, but rather they are a convergence of a number of 
factors. However, there are three things that are happening at the same time and 
are having a significant impact on change.<br>
<br>
Firstly, technology has provided new opportunities for people to learn in 
different ways and through a range of media. Secondly, the new generation has a 
different outlook on development. Thirdly, organisations are also rethinking how 
to measure the return on their investment when providing training-development 
activities.<br>
<br>
<b>Technology as a delivery mechanism</b><br>
In the early days of technology, there was a lot of discussion about how it 
could help provide the necessary training within an organisation and whether it 
would signal the end of the traditional training methods. Many organisations 
started providing e-learning courses and a lot of money was wasted. <br>
<br>
Regarding online training programmes, no matter how well they were presented, 
they were never going to replace the need for interaction between people. The 
providers of online training discovered that people started working their way 
through modules provided to them. Thus, while many had good intentions, it was 
after they had completed two or three modules that they decided to give up the 
rest. Research shows us that people did so because were bored working on their 
own and lacked the enthusiasm to complete the course. It was also discovered 
that people would dip into modules, and on closer examination, it was seen that 
people would look for specific answers to questions based on the issues they 
faced.<br>
<br>
For example, a person may have a particular problem with an employee for an 
issue relating to customer service; when that occasion arises, he would move 
through the modules seeking some guidance for an answer.<br>
<br>
<img border="0" src="manage1.jpg" width="350" height="216" align="right" hspace="6" vspace="4">Technology has, however, provided some significant methods of training and 
development opportunities that compliment the more traditional methods.<br>
<br>
For a start, the internet provides access to a host of information and enables 
the learners to conduct their own research. Many training programmes require 
delegates to conduct some work in advance. This enhances their preparation for 
by allowing them to formulate opinions, which can then be discussed with a group 
of delegates. Training providers also use technology as a method of providing 
supplementary information in addition to what people may receive when they 
attend a seminar, conference or a training programme. Even a quick look at the 
handout/workbook provided by most training programmes will inevitably reveal a 
reference to web sites where delegates can supplement their learning. <br>
<br>
Again, technology has provided an opportunity for many organisations and 
individuals to provide the learning opportunities through podcasting, You Tube™ 
and downloadable broadcasts on an iPod™ or MP3 player. After a quick look 
through iTunes™, an individual can download data like a broadcast from Jack 
Welch with his weekly thoughts on running a successful business, to that on 
various institutions such as the Harvard Business School. Technology has not 
taken away from the traditional methods of providing training and development. 
Instead it has added a new dimension and enhanced the delivery mechanism. The 
issue before providers of training and development activities is to maximise the 
potential that technology provides.<br>
<br>
<b>How people view their own development <br>
</b>Changes are taking place in the workplace as people plan to be in an 
organisation for only two to five years. We have a generation of people who are 
on the move, seeking to be an expert or a specialist. More and more people want 
to work for themselves and to subcontracting themselves to their employer to 
work for a period of time. The impact of this on training and development is 
very significant.<br>
<br>
The present generation sees learning and development as their responsibility and 
is not relying on the organisation to provide what they need. Many see the 
employer as only providing with what;’that employment’ needs from them. On the 
other hand, they are looking at what ‘they need’ to make them a specialist in 
their skill, which in turn, will make them a marketable commodity in seeking 
employment. The most successful people have always been the ones who have seen 
their development as their responsibility. Firstly, they see learning as an 
ongoing day-by-day activity that requires them to evaluate what they have done 
and seek better ways of doing it. Secondly, they seek to discover what training 
and development activities are available for them. They do not wait to be asked 
what they need or want, they are actively seeking and asking. If this is not 
provided by their employer, they will seek ways to find it for themselves. They 
are easily recognised because they read a lot, listen to material on-line and 
always ask questions.<br>
<br>
For those in the business of providing training and development, it means a 
significant shift in the way they perceived who their customers were. Many 
commercial training providers now face the problem of providing training based 
on what the employer requires and what the employee wants. The employer often 
wants an individual capable of doing one thing when the individual wants a lot 
more.<br>
<br>
<b>Employers’ need for return on investment</b><br>
The final challenge facing training and development is the organisation and its 
need to quantify how learning and development is providing return on investment. 
Training and development is not cheap, both in terms of raw cost to buy it and 
the lost productivity during the process.<br>
<br>
Organisations are seeking to use all sorts of elaborate methods in an attempt to 
quantify the return on investment, and the demands from the boardroom for the 
human resources department to substantiate costs are increasing. Many 
organisations still struggle with the idea that they may spend time and money 
training an individual only to lose the employee to a competitor. In reality, 
the more complicated and process-driven is training, the more it distracts from 
the outcome needed. Simple but specific objectives for any development 
activities will often enable the measurement to take place and to make the 
improvements.<br>
<br>
There is no certainty as to how training and development will look in the 
future. One thing we do know is that it will continue to be challenged to 
respond to the needs of the individual and then the needs of the organisation. 
Technology will provide many new opportunities but not final solutions. The key 
will be how responsive training providers will be to understanding their 
different customers, and those unique customer needs. </font>
