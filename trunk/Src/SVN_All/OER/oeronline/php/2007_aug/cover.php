<font face="Verdana" size="1"><b>CONSTRUCTION 
ACTIVITY</b>: <br>
Daewoo has begun the preliminary work</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover5.jpg" vspace="6"><br>
<font face="Verdana" size="1"><b>FISHING</b>: <br>
The only activity to support the local population</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover6.jpg" width="300" height="188" vspace="6"><br>
<font face="Verdana" size="1"><b>DWELLINGS</b>: <br>
Shanties will give way to modern housing infrastructure</font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">In the second phase of the Duqm project, landside and infrastructure facilities 
will be established under a separate contract, while a major ship repair 
facility will be constructed in the third phase. Construction of the ship repair 
yard and dry-dock will run concurrently with the Phase 1 development of the 
marine infrastructure.<br>
<br>
“The port of Duqm is being designed to serve a new industrial hub that is being 
developed in the region, part of the government’s drive to increase investment 
and employment opportunities across the country and spread the base of the 
economy away from the traditional centre around Muscat. One proposal currently 
being considered is a refining and petrochemical complex, with Oman 
Polypropylene having already completed a feasibility study for the project. 
There are already other repair yards in the region, notably those in Dubai and 
Bahrain. However, apart from being the most modern, the Duqm facility will also 
offer the advantage of being just outside the Gulf, meaning ship owners will not 
have to pay the substantial fees to enter those busy waters. Duqm will also be 
positioned to attract the wider Indian Ocean trade, an important consideration 
as shipping in the region increases,” says the report of Oxford Business Group.<br>
<br>
When operational around 2010, the Duqm dry dock will rival among the biggest in 
the region. It will provide direct employment to around 1,500 people on 
operation, and the workforce is projected to rise to 3,000 by 2018 if a proposed 
shipbuilding yard is added to the complex. Thousands of indirect jobs would also 
be created. Total investment in the ship repair yard is estimated at RO250 
million.<br>
<br>
According to government officials, the main advantages of having a port a Duqm – 
from a ship owner’s point of view – while compared to other ports in the 
vicinity is that Dubai and ASRY need about three days after-repair work in order 
to charge inert gas into cargo tanks and test the repaired parts. While Dubai is 
12 hours away from loading port, ASRY is a four-hour drive. Moreover, the ship 
owners have to spend 2.5 days of idle time in case their vessels are repaired in 
Dubai and ASRY. Singapore too has similar demerit in case of vessels discharged 
at Thailand. Duqm is, in that way, located at a better position. “Most 
importantly, Duqm will not have the problem of demand considering the maximum 
capacity of regional competitors. The amount of excess demand in the Middle East 
region in 2007 is estimated at 154 vessels,” says an official.<br>
<br>
<b>Refinery &amp; petrochemical complex<br>
</b>Al Wusta region is distinguished for having a great number of oil wells. One 
of the possibilities could be setting up a space-oriented industry such as oil 
refinery and storage. Gradual shifting of one of the refinery and export 
functions from Mina Al Fahal to Duqm is another possible option before the 
government. The second option gains ground based on the fact that the only oil 
export terminal of Oman at Al Fahal port has certain disadvantages such as it is 
surrounded by populated area, offshore area is also busy for commercial shipping 
activities, oil fields are far away and oil is transported over the mountains, 
and environmental issues arise from refinery work and marine operations. If 
these factors are taken into consideration, the future expansion of the oil 
export at Mina Al Fahal does not look a possibility, and Duqm seems be the right 
alternative.<br>
<br>
Also, the government is in talks with international companies about investing in 
the proposed refining/petrochemicals complex at Duqm. The project cost is 
estimated at US$7 billion. The company will be owned by a new JV to be formed in 
partnership between the government and international investors. The refinery 
will have a capacity to process 300,000 barrels a day using Oman export blend, a 
light crude. The oil will be transported to the refinery complex through a new 
pipeline from the country’s northern fields. Diesel output from the refinery 
will be exported, while fuel oil will be sold in the local market, according to 
an official. The refinery will comprise the world’s largest polypropylene plant 
with a capacity of 1.2 million to 1.5 million tonne/year, a 2.8 million tonne/year 
aromatics plant and a 750,000 tonne/year styrene plant. Construction of this 
complex is due to start at the end of 2008 and will be completed in 2012. 
However, according to the Ministry of Oil &amp; Gas in Oman, everything is at a 
planning stage and nothing has been finalised yet.<br>
<br>
<b>Duqm airport</b><br>
A new modern, civil airport is envisaged in the Duqm master plan. The region 
airport will come up on a span of 2,720 hectares, and will meet the industrial 
needs of the area. It will also complement the port facilities in the town. The 
government has recently asked US engineering consultancy firm Parsons 
International to advise it on the design and construction of this airport. 
Parsons will also assist the government in preparing tender documents, cost 
estimation and selecting an EPC contractor for building the airport. The 
required investment and other details of the airport project will be known only 
after the consultancy agency conducted a detailed study and design the airport.<br>
<br>
<b>Power &amp; desalination project</b><br>
The master plan envisages a mega power station. The first phase of the project, 
with a capacity of 63MW, will be completed by 2011. The capacity will be scaled 
up to 200MW during the later phases. The power generation facility will be made 
up of a number of existing diesel-powered generation plants that are proposed to 
be dismantled and installed at Duqm from their present locations at Salalah and 
Sur. Salalah has five such diesel-powered plants, while Sur has four. These were 
made redundant by the arrival of natural gas-based power generation, which now 
meets much of the Sultanate’s electricity demand. Besides, there’s a separate 
project for the construction of a 6,000 cubic-metres-per-day-capacity reverse 
osmosis (RO) desalination plant. According to officials, 5.6 million gallons 
water shall be available by year 2011. The Rural Areas Electricity Company (REAC), 
a subsidiary of the state-owned Electricity Holding Company SAOC, is overseeing 
the development of these projects.</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover7.jpg" width="300" height="228"><br>
<font face="Verdana" size="1"><b>NATURAL SPLENDOUR</b>: <br>
Duqm holds immense tourism potential</font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Tourism projects<br>
</b>A provision of 2,422 hectares has been made for tourism projects. 
Incidentally, three plots have already been taken up by the Oman Tourism 
Development Company (OTDC). KEO International Consultants has been selected to 
provide consultancy services for Duqm’s luxury hotel property. The project will 
be financed and managed by the Tourism Ministry’s investment arm, OTDC.<br>
<br>
The new hotel will be set on a 10-hectare plot located about seven kilometres 
from Duqm town, and not very far from the site of the Duqm Port &amp; Dry Dock 
complex planned at Ras Duqm. It will feature a 200-room hotel and 25 chalets, 
and will offer all the facilities and amenities of a modern international-class 
hotel. Business facilities, conference rooms, retail outlets and a mini golf 
course will add to the hotel’s appeal to visiting businessmen, consultants, 
contracting executives and investors. The property is expected to open for 
business in November 2008.<br>
<br>
<img border="0" src="cover8.jpg" align="right" hspace="6" vspace="4">With these kinds of mage projects in the pipeline, even the locals are upbeat. 
“The construction of Duqm will help in achieving a number of economic and 
tourism objectives and will be an added value to the services currently provided 
by Sultan Qaboos Port, Salalah Port and Sohar Port. The construction of the port 
will also create a considerable number of job opportunities for Omanis in 
general and Duqm locals in particular. It will also boost fish related industry, 
which is one of the major sources of GDP and the source of living for a 
considerable number of families in the region,” Sheikh Helal Bin Ali Al Habsi, 
Wali of Duqm, said.<br>
<br>
A number of health facilities and service utilities will be implemented at the 
wilayat to meet the growing demand for such services especially the area is 
expected to witness a remarkable boom in a number of industries. These utilities 
will provide valuable services for both locals and visitors, he furthered. <br>
<br>
Overall, Duqm will be first comprehensive planned town of the Sultanate of Oman. 
All the land use and activities are controlled and regulated. Location of 
industries and other land uses have been proposed keeping the natural 
environment in mind. The facilities that would be coming up in this city would 
be modern, sophisticated and up to date as far as the technology is concerned. 
Once completely operational, this town would be a force to reckon with in the 
entire Gulf region. No doubt, then it will be a destination for thousands of 
people, including the migrants.
</font></p>
