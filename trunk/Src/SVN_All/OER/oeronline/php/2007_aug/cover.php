<p align="justify"><font face="Verdana" size="1"><b>Residential town area</b>
<br>
There will be 17 residential quarters in place to accommodate a population of 
64,000 in a span of next
20 years. There is a provision for further expansion of the town comprising 
about 2,530 hectares to
111,000 residents. The first phase of the town will see an investment of RO110 
million. Each of these
17 quarters is a 1kmx800m rectangular block. Each quarter accommodates on an 
average about 4,750
residents. It will have an average of about more than 500 plots as parcel. More 
than 90 per cent of
these plots would be allotted for individual villas with three categories of 
housing. Only 10 per cent of
the plots are multi family housing with five storied apartments. All social and 
recreational facilities are
being designed within each of these quarters.<br>
<br>
About four quarters, along with local centres, should be available by 2011. The 
local centre shall contain the following facilities: Kindergarten, health centre (optional), a mosque, 
community recreational
centre, children’s play area, private school/training centre, post office 
(optional), shops and a petrol
filling station (optional). The town will also have three district centres. Main 
components of the district
centre will be grouped around a main square. Main attraction of the town would 
be the composite
Town Centre, which would act as the multipurpose central business centre of the 
city. The first phase
of the Town Centre, to come up on a spread of 172 hectares, will be ready by 
2011. It comprises retail
shops, restaurants, government and private offices, business centre, recreation 
facilities, cinema halls,
shopping malls, traditional souq, hotels, regional police headquarters, town 
mosque and regional and
the town bus terminals. There shall be adequate parking facilities for major 
land uses, including public
parking areas.
<br>
<br>
Apart from these, there will be a large Town Park over a 119-hectare area. It 
would have city’s sports
centre along with a large stadium. One private investor has shown interest in 
putting up a labour camp
and a residential accommodation for junior and mid level officials. An 
integrated large business centre
in the main Town Centre of Duqm is also planned with an estimated investment of 
RO300 million.
Though there is already a talk of booming real estate in the area, a 
Muscat based real estate agent
clarified that the government had banned all buying and selling of properties in 
the area as too much
speculation about the new mega projects might lead to price escalation. “We have 
this experience of
Sohar, where biggies bought the land and sold at higher prices. Probably to 
avoid this, the government
wants to keep the real estate prices in check. Moreover, the government itself 
wants to be part of overall
development of Duqm,” says the agent.</font></p>

