<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">
Al Duqm</font></b><font face="Verdana" size="1"><br>
</font>
<font face="Verdana" size="4">
<b>Breathtaking Development<br>
</b>
</font>
<font face="Verdana" size="1"><br>
<b>Al Duqm is hardly a destination for the average tourist or leisure-traveller, 
leave alone industrialists. But come 2010, Oman’s most comprehensively planned 
town will blip on the global industrial radar like no other Omani city. P Aneel 
Kumar takes you to the road to Duqm</b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<img border="0" src="cover1.jpg" width="300" height="216"></font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">During the period of what they call ‘the flood’, the miniscule population of Al 
Duqm migrates to northern parts to find some work in date fields. During this 
period (from June to September), the temperature drops below 30 deg C, which is 
unsuitable for fishing – the lifeline of the town. Perhaps by next year, this 
‘summer journey’ might stop. For the topography of the area is up for some 
makeover – a makeover worth hundreds of millions of riyals.<br>
<br>
What is considered now as a sleepy, fishing town is soon expected to eclipse the 
Sultanate’s fast-developing industrial hub, Sohar. It might sound like an 
exaggeration, but Duqm, if all goes according to the master plan, will be a 
force to reckon with in the entire Gulf region in the days to come. Though only 
bits and pieces of information about Duqm’s development as a new industrial 
destination is available, the plans for development of the area include a crude 
oil export terminal and a strategic storage facility, a free trade zone and a 
downstream industrial area in addition to an airport, port, dry dock, commercial 
and residential areas and plans for tourism development. Thanks to its proximity 
to the busy regional sea-lanes traversing Oman’s coastal waters, Duqm – about 
700 kms from Muscat – is being conceived as a main maritime gateway that will 
serve an ambitious industrial and commercial hub. Apart from the strategic 
location of Duqm, the friendly climate of the area adds to its advantages.<br>
<br>
The purpose behind developing Duqm is to export Gulf’s crude oil from an Omani 
port. Outline of the plan is to export crude oil, which is produced in the Gulf 
region and transported through pipeline to Omani port. Positioned as it is on 
the Gulf of Oman, with the Strait of Hormuz and the Arabian Sea at its 
north-eastern end, and with its long coastline running south along the Arabian 
Sea, the government believes Duqm is strategically the most preferred location 
for this port.<br>
<br>
The biggest catch for Duqm could be its mineral export capability. The total dry 
bulk cargo handled at Duqm in 2025 is estimated to be four million tonnes. The 
exports of aggregate will comprise limestone for steel industry and limestone 
fillers for other industries as well silica sand/quartzite. Most importantly, 
the port will handle a substantial portion of the fish export from Oman. It is 
estimated that in 2025, the total fish catch of Sultanate of Oman will be 
250,000 tonnes, and about 50 per cent of it will be exported. Share of the catch 
and the export from the ports are likely to be like this: Sultan Qaboos – 35 per 
cent, Duqm – 27 per cent, and Salalah – 13 per cent.<br>
<br>
Not just this. Al Wusta Region is distinguished for having a great number of oil 
wells. One of the possibilities could be setting up a space-oriented industry 
such as oil refinery and storage. “As the Sultanate is also looking to further 
strengthening its links with the sea and maritime trade, it is seeking to 
position itself as the region’s leading ship-repair centre by constructing a 
massive new dry dock and maintenance facility. The Sultanate has already poured 
billions of dollars into developing the ports of Muscat, Sohar and Salalah to 
boost the cargo handling capacity. With Duqm, it is now investing to make Oman 
the port of choice for vessels in need of repair or overhaul,” according to a 
report by OBG.</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<img border="0" src="cover2.jpg" width="412" height="259"></font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Master Plan of Duqm<br>
</b>A comprehensive master plan has been prepared for Duqm by the Supreme 
Committee for Town Planning, Sultanate of Oman. The area will have a major dry 
dock, commercial port and a shipbuilding yard in future. The master plan has 
designated areas for airport, petroleum refinery and oil storage facilities 
along with downstream industries, a fishing harbour with fishery related 
industries, free trade zones, general industries and tourism projects. It has 
planned a complete residential town with a Town Centre and all modern amenities.
<br>
<br>
A tentative allocation of 1,026 hectares has been made and designated as the 
Free Trade Zone. However, the total area in five different locations within the 
Duqm development area available both for FTZ and general industries is about 
21,000 hectares. Out of this, about 3,300 hectares has been designated for the 
study of setting Duqm refinery and petrochemical complex, and about 1,300 
hectares for fishery related industries. The commercial and dry dock facility 
would come up on 237 hectares. While the main port area would come up on a site 
of 115.6 hectares, ship repair facility would have 102-hectare area. It also 
will have about 13.4 hectares for workers and shipping clients to stay and rest. 
Exploring opportunities in tourism is one of the main components in the master 
plan, which includes hotels, resorts and a public beach.<br>
<br>
<b>Port &amp; dry dock facility<br>
</b>Duqm port is to be established to serve domestic trade, tourism and the 
fishing industry. Phase I provides for the harbour basin to be dredged to a 
depth of 10 metres and for the construction of 450 metres of jetties and a dry 
dock for ship repairs and maintenance. The port will be able to receive ships of 
up to 100,000 tonnes. Covering an area of 1.163 million square metres, the 
facility will comprise two dry docks as well as a floating dock equipped to 
repair and maintain oil and gas tankers of 100,000-tonne capacity. The port will 
have a total length of 2,800m for mooring and berthing of 8–10 vessels. It will 
simultaneously have berthing facilities for up to 16 vessels of small and medium 
size.<br>
<br>
The complex will have two dry docks to repair VLCC/LNG carriers, six different 
length size quays for berthing and mooring of up to 16 vessels simultaneously, 
heavy zones for carrying heavy materials with 150-tonne mobile crane and heavy 
structure handling up to 10,000 MT, unloading &amp; stocking material for unloading 
and stocking of major material, steel plates, copper slag (Nix), Hull processing 
shop, a work shop with newest processing and machining equipment, electric motor 
shop for repairing LNG carrier, other utilities like oxygen, acetylene, CO2 and 
compressed air plant, and a power distribution centre for nine sub stations. As 
part of environment friendly measures, there will be chemical oil treatment 
plant, Nix recycling plant, sewage treatment plant, incinerator plant and fresh 
water pump station. The welfare facilities include worker’s dormitory, guest 
house, sports centre, restaurant and playground. Dry dock and the commercial 
port are expected to be operational by 2010.<br>
<br>
An agreement worth RO2.15 million was signed on June 26, 2005, between the 
Government of Oman and South Korea’s Daewoo Shipbuilding and Maritime 
Engineering Company for provision of consultancy services and preparation of 
engineering designs leading to the construction of the dry dock facility.<br>
<br>
Having the reputation of being the world’s second largest shipbuilder, Daewoo 
has been awarded the contract for the design of two dry dock facilities and a 
floating dock facility, in addition to providing consultancy services for 
machinery. In late September, Oman awarded the contract to oversee the 
construction of the facility to Daewoo in a separate agreement. As part of the 
agreement, Daewoo will also manage and operate the repair docks for a 10-year 
period after its completion, on behalf of a newly formed state enterprise, the 
Oman Dry Dock Company. At the time the contract was signed, Ahmed bin Abdul Nabi 
Macki, Oman’s minister of finance and national economy, said the dry dock 
facility was one of the major strategic initiatives being undertaken by the 
government to develop new sources of revenue and employment for the country and 
diversify the economy. While the initial focus of the yard is repair and 
maintenance work, shipbuilding has not been ruled out.<br>
<br>
In April 2007, an Omani-Turkish consortium was awarded a contract to develop the 
marine infrastructure for the new Duqm port complex at a cost of RO186.892 
million. Consolidated Contractors Company (Oman) and Sezai Turkes – Feyzi Akkaya 
Ltd (STFA) of Turkey will execute the project in joint venture with 
Belgian-based international dredging giant Jan De Nul Dredging Ltd. The contract 
covers the first phase development of the Duqm port. This phase, comprising the 
maritime works package, covers the construction of breakwaters and quay walls, 
dredging and reclamation works and two breakwaters of a length of around 5 
kilometres. A pair of quays will also be constructed – a 700-metre-long quay for 
container, general cargo and dry bulk vessels, and a 330-metre quay for 
government vessels. Berths for tug and pilot boats and other craft will be built 
as well.<br>
<br>
In addition, dredging will be carried out to provide for the entrance channel, 
harbour basin, construction pockets and turning circle, with deeper dredged 
pockets created at the berths and floating docks. The harbour basin will be 
dredged to a depth of -16 metres and the approach channel to -17 metres. Also, 
as part of the maritime works package, reclamation will be undertaken behind the 
breakwaters, quay walls and inshore areas. Allowance must also be made within 
the commercial quay for the potential future installation of electrically 
operated rail-mounted quayside cranes.</font></p>
