<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">It’s in the pipeline<br>
</font><font face="Verdana" size="1">Petroleum Development Oman (PDO) has 
established a network of more than 16,000 kilometres of pipelines and flowlines 
through which the crude oil and natural gas that it produces are delivered to 
customers on behalf of its shareholders. OER looks at the challenges in the 
incredible journey of oil to the coast and of gas to the south</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="pdo.jpg" width="400" height="300"><p align="justify" style="margin: 3px 10px">
<b><font face="Verdana" size="1"><br>
<br>
</font></b><font face="Verdana" size="1">PDO operates a total length of about 
16,000 kilometres of pipelines and flowlines ranging in diameter from 2 inches 
to 48 inches. It is no exaggeration to say that Oman’s lifeblood of oil and gas 
flows through this network. No wonder, then, that PDO ensures that these assets 
achieve the highest levels of performance and meet demanding levels of technical 
integrity. One of the main commitments of the pipeline group in PDO is to 
minimise the leak rate of an aging facility. “This is achieved by making sure 
that maintenance is carried out in a cost-efficient manner. We have succeeded in 
extending the life time of our pipelines way beyond their design lives,” says 
Maqbool Hussain al Zadjali, Infrastructure Pipeline Systems &amp; Projects Manager, 
PDO. The pipelines in PDO are categorised in four main groups: the Main Oil Line 
(MOL) system, the South Oman Gas Line (SOGL) system, inter-field pipelines and 
field flowlines (see box – Description of the MOL &amp; SOGL Systems). </font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b><br>
Operation and transportation challenges<br>
</b>The MOL and SOGL sections were originally designed to last 20 years. 
However, by implementing a stringent integrity management system, PDO has 
extended the lifetimes to an average of 30 years for the MOL segments and 35 
years for the SOGL segments. Zadjali informs: “This was made possible due to the 
implementation of the Risk-Based Inspection methodology and Corrosion Management 
System to optimize the integrity of the transportation system.”<br>
<br>
<b>Elevation</b>: To reach the MAF tank farm where it is held before being loaded onto 
ships, Omani crude has to cross some mountains. The MOL reaches its high point 
(approximately 670 metres above sea level) at Wilaya Izki, and from there runs 
down hill all the way to the terminal. At the current flow rate, the high point 
creates a static head at MAF that is significantly greater than the frictional 
resistance of the pipeline. Hence, pressure reduction at MAF is necessary to 
protect the tanks and its inlet piping. It is also essential to maintain a back 
pressure at the high point of not less than two atmospheres, to avoid having gas 
bubble out of the crude. <br>
<br>
<b>High viscosity</b>: Some of Oman’s fields – including Occidental’s Mukhaizna field 
and PDO’s Qarn Alam field – contain dense, viscous oil. Once the enhanced oil 
recovery projects at Mukhaizna and Qarn Alam come on stream, their production 
will change the properties of Oman’s export crude, making it somewhat heavier 
and thicker. It is important to take this into account when verifying the 
existing MOL system capacity. <br>
<br>
<b>High water content</b>: The age of Oman’s major oil fields, some of which have been 
producing oil for more than 40 years, is posing another indirect challenge to 
the MOL system. As oil fields age they tend to produce more water with their 
oil. Such water is a major corrosion threat to the system. The increasing 
amounts of water in oil will make it difficult for processing stations to meet 
the specifications for entry to the MOL system. <br>
<br>
<b>Third-party activities</b>: Most of the MOL and the SOGL lies buried and out of 
sight, but protecting the systems from accidental damage by third parties still 
presents a challenge. Such protection is particularly critical wherever the MOL 
and SOGL systems pass through inhabited areas. One classic example is the 
construction of the new major southern highway in the Muscat area. The route of 
the highway crosses the MOL pipeline at 14 locations. Stringent controls are put 
in place to safeguard the pipeline during the construction activities. </font>
