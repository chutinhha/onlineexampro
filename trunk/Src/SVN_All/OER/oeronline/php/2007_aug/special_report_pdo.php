          <font face="Verdana" size="1">
<b>Description of the MOL &amp; SOGL Systems<br>
          MOL</b>: The MOL is a pipeline system that transports liquid hydrocarbons from 
the interior of the country to PDO’s Mina al Fahal (MAF) oil export facilities 
on the coast. The MOL consists of 1,536 kilometres of pipeline, varying in 
diameter from 6 to 48 inches with a maximum system capacity of approximately 1 
million barrels a day. The transported liquids include PDO crude oil, 
third-party crude oil and government condensate (a liquid hydrocarbon that 
precipitates out of natural gas when the gas pressure is reduced). The MOL 
system has a strategic importance as it is the only means of evacuating the 
liquid hydrocarbons from interior production stations to the exporting 
facilities at MAF. Furthermore, the MAF terminal is the sole means of exporting 
Oman crude to the world market.<br>
<br>
          <b>SOGL</b>: The SOGL pipeline system is used for the transportation of “dry” gas 
(i.e., gas without condensate or water) from the central Oman gas fields to the 
gas consumers in the south. The system consists of 720 kilometres of pipeline 
varying in diameter from 2 to 16 inches. It consists principally of two sections 
– a 293-kilometre, 16 inches line from Saih Nihayda to Hubara and a 
159-kilometer, 10 inches line from Hubara to Marmul. This system transports gas 
for use in PDO’s oil-production operations, including gas injection (for 
reservoir pressure maintenance) and power generation.</font></td>
