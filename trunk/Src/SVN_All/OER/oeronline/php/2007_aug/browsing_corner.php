        <p align="justify"><b><font face="Verdana" size="4"><br>
        No Big Deal</font><font face="Verdana" size="1"><i><br>
        </i>Harvard Business School Professor Kotter and technology executive 
        Rathgeber share lessons about change in a corporate fable<i><br>
        <br>
        </i>
        </font></b><font face="Verdana" size="1">
        <img border="0" src="bc.jpg" align="left" hspace="6" vspace="4">Remember the story about the greedy fox and
the gullible lamb on the waterfront? How
about the smart mouse and the clueless lion?
These classic fables, the staple of many a childhhood,
are now graduating to prestigious business
schools, with teaching professionals using them
to make their lectures more interesting. In fact, it
is just a question of time before they are converted
into handy business tomes for practitioners across
the globe.
<br>
        <br>
        And, that is precisely what Harvard Business School
dons, John Kotter and Holger Rathgeber, have tried to
attempt in ‘Our Iceberg Is Melting: Changing &amp; Succceeding
Under Any Conditions’, their 2005 150-page
‘picture’ book that has only now hit the bookshelves
in Oman. The title gives away the theme. But, what
is most noteworthy is their resort to the Aesop fable
format – using penguins of the Antarctic as the main
characters – to convey the underlying theme.
<br>
        <br>
        Kotter and Rathgeber weave a nice story of how a
group of penguins – with nomenclatures, yes! – are
faced with a life-threatening crisis: the iceberg
they inhabit is on the verge of disintegration and
how their leadership responds to this situation by
winning over the majority and survives the scare.
It is a tough call. Replace penguins with you and
I – and the situation is nothing out of this world or extraordinary. It is realistic to the core. 
Business esttablishments
run into bumps at regular intervals and
one cannot also discount the presence of someone
trying to score a brownie point.
<br>
        <br>
        The Kotter-Rathgeber duo chalks out an eightstep
recipe to face the challenge (See box).
Honestly, there are no great insights in this pageturner.
Whatever is said is all known because these
tenets are very commonsensical. If you believe that
commonsense is most uncommon, then this book
is for you. Otherwise, forget it.
<br>
        <br>
        – <b>Ramesh Kumar</b></font></p>
