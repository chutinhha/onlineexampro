<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Enter taxation:</font></b><font face="Verdana" size="1"><br>
</font>
<b><font face="Verdana" size="4">Bahrain’s insurance for the jobless</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1"><br>
<b>The scheme seeks to address unemployment consequences by extending financial 
lifeline besides training and job opportunities to unemployed nationals, writes 
Dr Jasim Husain Ali</b><br>
<br>
<img border="0" src="closeup1.jpg" width="120" height="228" align="left" hspace="6" vspace="4">No topic has generated more interest in Bahrain’s recent history than that of 
insurance against unemployment initiative. The scheme has proven to be the most 
discussed issue in public circles in the last few months and for good reasons, 
especially as employees must contribute one per cent of their salaries to the 
project. In short, many feel it is not appropriate for officials to ask them to 
resolving a problem that they have not caused. Bahrain has been suffering from 
the unemployment problem for sometime now. While the actual number of unemployed 
remains a mystery, the estimate ranges from 8 to 15 per cent depending upon the 
source.<br>
<br>
<b>Financial lifeline</b><br>
The scheme seeks to address unemployment consequences by extending financial 
lifeline besides training and job opportunities to unemployed nationals. Under 
it, first-time job seekers and those who lose their jobs are entitled to monthly 
financial support. Those with university degrees receive BD150 per month (nearly 
US$400) while those without college qualifications are entitled to BD120. Also, 
insured employees working in either public or private sector establishments 
receive more generous compensation, equalling 60 per cent of their salaries, on 
the basis of average wages over the last 12 months. However, this amount cannot 
be less than BD150 nor exceed BD500 per month. <br>
<br>
The scheme went into effect from June. But, qualified beneficiaries will only 
receive their first payments starting from late November, six months after the 
scheme took effect. The delay is attributed to the need of generating sufficient 
funds. The scheme is expected to collect around BD36 million (US$95 million) in 
2008, its first full-year, but only BD21.5 million in the first seven months of 
2007. However, actual revenue and therefore spending are likely to change over 
the next few years, depending on factors such as increases of salaries and the 
actual number of unemployed. <br>
<br>
In all cases, the financial compensation extends to a maximum of six consecutive 
months; with the beneficiaries losing support in the case they refuse two job 
offers that are commensurate with their qualifications. Thus, the authorities 
have committed themselves to providing proper training for job-seekers. Other 
terms clearly state that the beneficiary must be a Bahraini citizen, aged at 
least 18, and not engaged in a private business. Nevertheless, critics have 
raised doubts about the ability of the ministry of labour of providing job 
opportunities, meeting the desires and the qualifications of the jobless, within 
six months. At any rate, the government is required to continue providing 
financial support beyond the six-month period in case it fails to offer proper 
jobs. <br>
<br>
Women stand to be main beneficiaries of the scheme, as they constitute the 
majority of the local jobless, especially as they do not appear to be popular 
with private sector firms. In 2006, 10 per cent of the employees in the private 
sector and 43 per cent in the public sector were women. Clearly, the scheme 
seeks to provide a lifeline to women by giving them some sort of financial 
independence. <br>
<br>
<b>Financing, hot issue<br>
</b>Popular discontent against the scheme relates specifically to its financing 
mechanism. The project is to be financed through insurance subscriptions 
provided by three parties – the government, employees and the employers. Under 
it, three per cent of the salary of all employees, including foreign workers, 
will go into the scheme. However, no justification has been given by the 
authorities as to how it arrived at the figure of three per cent. Some 
commentators have suggested that only a two per cent deduction would have been 
sufficient in meeting the scheme’s objectives. <br>
<br>
To be sure, the government side is burdened with ensuring the bulk of the 
financing for the scheme–contributing as it does nearly half of the cost. Thus, 
it has to contribute one per cent for private sector employees, as the 
authority, and two per cent for public sector employees (one per cent each for 
being the authority and the employer). Against this, employees, whether in the 
public or private sector establishments, contribute only one per cent. Also, 
there is no real additional burden on the employers, as their share will come, 
albeit indirectly, from a special fund dealing with training locals, known as 
the Labour Fund. Clearly, this is the reason why the business community has not 
opposed the project. <br>
<br>
<b>Expatriates pay as well</b><br>
Again, expatriate employees have to contribute to the scheme in order to avoid 
making them more popular with their employers at the expense of citizens. In 
fact, private sector firms are noted for employing foreign workers as opposed to 
locals. Expatriates comprised 81 per cent of total employment – around 313,000 – 
in the private sector in 2006. Yet, unlike locals, unemployed expatriates are 
not entitled to benefits on the grounds that they should have specific working 
contracts to be able to stay in the country. To compensate, the expatriates will 
receive the value of their participation in the scheme when they leave the 
country following termination of their contracts.<br>
<br>
<b>Dismay and anger </b><br>
Despite such steps, the locals continue to be disenchanted with the scheme and 
argue that the government must bear all, or at least the majority of the 
scheme’s financing. They base their argument on the fact that ultimately it is 
the authorities that are responsible for the jobless problem, given its 
conscious economic decisions. <br>
<br>
Again, some are dismayed at the idea of providing a financial life line to the 
unemployed, especially for first-time job seekers to collect a monthly 
compensation. Similarly, the argument goes that contributors were forced to take 
part in the scheme, much against their will. Other critics had sought 
clarifications and edicts from senior religious figures, notably outside the 
country, about the sanctity of the scheme. Here, some replies said the scheme 
lacked any religious basis. Clearly, the reaction to the scheme has been 
disproportionate.<br>
<br>
A good number of people have turned their anger towards the members of the 
elected parliament, including me, the author, for endorsing the bill, which was 
introduced in the form of a royal decree. However, it must be remembered that 
MPs are not allowed to amend any part whatsoever of any royal decree. 
Ostensibly, the authorities opted for this option to avoid lengthy deliberations 
on the issue. <br>
<br>
Nevertheless, reflecting exceptional popular discontent, there remains the 
possibility of amendments being introduced to the financing element of the 
scheme in the next few months, pending negotiations of parliamentary blocs with 
the authorities. Proposals include doing away with the regressive tax regime or 
applying the same rate on employees regardless of revenue. As such, there is a 
possibility of the regime being revised by applying different tax rates on 
dissimilar income distribution. Still, another proposal calls on the authorities 
to introduce a system of annual bonuses, or special income, to be extended to 
families meeting the criteria. <br>
<br>
<b>Model for others, maybe<br>
</b>Fellow countries in the regional grouping of Gulf Cooperation Council (GCC) 
may have to follow Bahrain’s suit. Oman, for one, may have to copy the model, as 
unemployment amongst locals is a serious issue. In fact, the challenge of 
unemployment appears to be common to both Bahrain and Oman. The issue has become 
only more pressing to Oman following the devastating cyclone Gonu that hit the 
Sultanate in June. Gonu left many people dead and injured and 67,000 homeless 
besides causing damages between US$2.6 billion and US$3.250 billion.<br>
<br>
Again in Oman, similar to Bahrain, creating a sufficient number of job 
opportunities for locals has always been a problem. Also like that of Bahrain, 
the Omani population is notably young – nearly 40 per cent of the nationals are 
below the age of 15 years. In other words, Oman cannot afford to overlook 
unemployment consequences. Certainly, it is risky to leave the jobless without a 
financial lifeline. <br>
<br>
To conclude, the authorities do not see the scheme as taxation while others 
regard it as a form of taxation. To officials, this is a one-off programme, 
designed to address an issue of paramount significance. <br>
<br>
Officials believe real taxation can only be introduced in conjunction with 
coordinated efforts with GCC member states. For one, the International Monetary 
Fund (IMF) has been advising of the need for introducing taxes in order to 
broaden revenue sources and thus becoming less dependent on oil. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
