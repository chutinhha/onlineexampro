<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Little Landy</font></b><br>
<font face="Verdana" size="1"><b>The latest addition to the revitalised fleet of 
Land Rover, the LR2, comes as a welcome surprise and brings with it many new 
features</b><br>
<br>
<br>
<img border="0" src="auto.jpg" width="300" height="247" align="left" hspace="6" vspace="4">After easing its entry into the Land Rover segment with the Freelander in 2001, 
Ford’s PAG division is certain to repeat history with the new LR2. At first 
sight, the LR2 seems much like a smaller Range Rover. The colours and side ports 
are similar to its big brother. It’s quite evident from the size that it was 
built to replace the Freelander. But there are enough contrasts to give the LR2 
its unique feel.<br>
<br>
There is a remarkable improvement in the styling and build of the LR2 as 
compared to its predecessor. The interior is simple, rugged and stylish. The 
seating capacity of five is quite comfortable. There are striking similarities 
with the Range Rover, including the Terrain Response System, which is standard. 
This feature is a fine addition to this model range since it makes off-roading 
as easy as changing the knob, while pretty much everything else is taken care of 
by the system.<br>
<br>
The LR2 comes with a 3.2L straight-six giving out 230bhp. It features all the 
latest technologies, giving 30 per cent more power and 10 per cent better fuel 
economy than the previous 2.7-litre, V6 engine in the Freelander. The standard 
model also comes with a six-speed automatic transmission that is a bonus for the 
segment and price range of this car.<br>
<br>
The explanation of the features vs. price ratio can be described by the 
strategies adopted by the parent company, Ford. The LR2 shares a lot of its DNA 
with the Volvo S80, primarily the platform and some of the front suspension 
components.<br>
<br>
<b>Off-road ease</b><br>
The Land Rover company incorporates the latest off-road system in the LR2. The 
Range Rover is one such successful application. There are some changes in the 
off-road system, mainly the unavailability of the rock-climbing mode. The rest 
are covered, making this a good off-roader in any driver’s hands.<br>
<br>
From the driver’s seat, the LR2 offers a good view of the road ahead with a low 
dashboard. This platform has a lower centre of gravity than the old Freelander 
chassis. However, larger wheels and tyres cause a small overall increase in the 
LR2’s tipping point. A wider track offsets the gravity issue. In short, the LR2 
feels a lot more firmly planted than the Freelander did.<br>
<br>
Additionally, it has a longer wheelbase – 40 per cent more for torsion rigidity 
and a fully independent suspension. Land Rover says it tested and tuned the 
LR2’s handling at the Nürburgring racetrack in Germany. Well, at home the 
outcome was more than adequate. <br>
<br>
Land Rover is one of the oldest companies dedicated to building 4WDs. The 
Defender continued to stand as a symbol of the company for decades. To see a 
company come this far begs the question, “Do they still have it today?” The 
answer would simply be “yes”. They haven’t lost their values and continue to 
build off-road marvels like the Range Rover. The LR2 might not be as good as the 
Land Rover brethren, but its capabilities are much more for its size and price 
than the rest of the competition. This is probably the best executive entry 
level 4WD-cum-SUV you could buy today.</font><p align="justify" style="margin: 3px 10px">&nbsp;<div align="center">
