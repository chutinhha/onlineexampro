<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Fill your glass with Barry Meher</font></b><font face="Verdana" size="1"><br>
<b>An NPA Events’ Presentation</b><br>
<br>
<b>Considered to be one of the leading experts in his field, Barry Maher is a 
consultant, an author and a speaker whose efficacy shows up on the bottom lines 
of the professionals and organisations who attend his talks and seminars, or 
read his books<br>
</b><br>
<img border="0" src="manage3.jpg" width="250" height="230" align="left" hspace="6" vspace="4">On 
the September 2, NPA Events will be bringing Barry Maher to Muscat to conduct a 
one-day workshop, ‘Filling the Glass for Leadership’ at the Ball Room, Shangri 
La’s Barr Al Jissah Resort &amp; Spa.<br>
<br>
Based on his bestselling book ‘Filling the Glass: The Skeptic’s Guide to 
Positive Thinking in Business’, the workshop will incorporate interactive real 
world tactics and reality-based motivation for increasing productivity and job 
satisfaction. Workshop attendees will gain insights for professional 
development, motivation, work-life balance, management, leadership corporate 
culture, workplace issues, positive thinking and personal development.<br>
<br>
Barry Maher doesn’t agonise over whether the glass is half-full or half-empty, 
his stated mission is to tell people how to fill the other half of the glass. He 
does this with wit and humor. “The person I want to be, the person I want to 
hire, and the person who ultimately will be more successful and more valuable to 
his company, his family, his society, and himself is the one who takes a look at 
that glass and is concerned, not with whether it’s half-empty or half-full, but 
with figuring out how to fill it up.”<br>
<br>
Barry Maher started out in sales, and became highly successful at that, then 
progressed with motivational selling, and consultancy to numerous leading 
corporations. <br>
<br>
He offers ten strategies for Filling the Glass, provides many supporting case 
studies, examples, brief tips, detailed tactics, with a few parables.<br>
&nbsp;<br>
To quote:<br>
1. Make peace with the negatives. Every situation has its negatives; make them 
work for you.<br>
<br>
2. Fill the glass. Attitude is important, but reality rules.<br>
<br>
3. Become your own guru. You’re your own best guide. The most important 
decisions take place when Stephen Covey can’t be with you.<br>
<br>
4. Add water. Far too often we keep our strongest selling points hidden. Leave 
ways to promote your ideas, your projects and proposals, and ultimately, 
yourself.<br>
<br>
5. Bring out the prospect in yourself. Look inside and outside for empathy and 
understanding.<br>
<br>
6. Become an expert witness. Let the other side make its points – and make yours 
convincingly.<br>
<br>
7. Fail towards success. Become a master craftsman, a master craftsman being 
someone who’s already made every possible mistake.<br>
<br>
8. Brag about the negatives. Make your biggest liabilities your strongest 
assets.<br>
<br>
9. Change the scale to make the sale. Size may count, but it’s not how big it 
is, it’s how big it seems.<br>
<br>
10. Never settle for success. Keep your goals from getting in your way; make 
your journey even more enjoyable than the destination.<br>
<br>
Maher claims not to want to be confused with Stephen R. Covey. Nevertheless, one 
of the many things they both have in common is a belief in a restoration of the 
character ethic.<br>
<br>
Maher expresses a preference for becoming one’s own guru rather than developing 
a dependence on the motivational kind whose influence often fails the test of 
time and reality. His advice: “Never let your positive thinking obstruct the 
reality in a haze of wishful thinking. Acknowledge the reality: perceive it as 
accurately as possible, so it can be dealt with effectively. Always realising 
that – as the gurus keep telling us – we are responsible for our own attitudes 
and our own happiness.”<br>
<br>
On defining what one’s real goals are, he says, “Filling the glass is about 
reconciling the disconnects between what you’re actually doing and what you 
believe you should be doing. (It) doesn’t mean that you won’t have to make 
sacrifices to get what you want. It simply means that you make certain you 
actually want whatever it is you’re making those sacrifices to achieve.” And, 
“…the key is to pursue the goals you really want to pursue. Not the goals you 
think you should have. Not the goals your boss or your parents … or even your 
(spouse) think you should have. Not the goals you have because you feel you have 
to keep up with the Joneses, or because somebody else might decide that you’re 
not a success.”<br>
<br>
Aiming to maximise both productivity and career satisfaction, Maher has worked 
with many of the world’s most successful organisations, including ABC, the 
American Bar Association, the American Management Association, AOL/Time Warner, 
AT&amp;T, Blue Cross, Budget Rent a Car, Canon, Colgate-Palmolive, Epson, Fuji Film, 
Johnson &amp; Johnson, Merck and Verizon. When it comes to motivating and training 
salespeople, as Selling Power magazine said, “To his powerful and famous 
clients, Barry Maher is simply the best sales trainer in the business.”<br>
<br>
Today’s Librarian named his book, Filling the Glass: The Skeptic’s Guide to 
Positive Thinking in Business, “[one of] The Seven Essential Popular Business 
Books” along with classics like Getting to Yes, Seven Habits of Highly Effective 
People and Guerrilla Marketing. For executives, managers, entrepreneurs, 
salespeople, employees of all types, the strategies of Filling the Glass are 
“strategies that can work for all of us in business – and even in life.” He is 
also the author of The Prentice Hall Marketing Yearbook and the mini-cult 
classic novel, Legend. His latest book, No Lie: Truth Is the Ultimate Sales 
Tool, has been translated around the world.<br>
<br>
Maher has appeared on several TV shows such as the Today Show, NBC Nightly News 
and CNBC reports and has been written about in publications ranging from the New 
York Times, USA Today, the Wall Street Journal, Business Week and the London 
Times.<br>
<br>
Maher stated that, “This is my first visit ever to the Arabian Gulf region. I 
have been reading up on Oman and it seems to be a really beautiful country. I am 
really looking forward to my visit and I’m especially keen on meeting business 
professionals from Oman.”<br>
<br>
NPA Events who will be organising and managing the entire event are reputed for 
quality events in the field of entertainment and management. For more details on 
Filling the Glass for Leadership with Barry Maher please contact Mahira Saqib – 
NPA Events, Events Coordinator on 95039651.<br>
