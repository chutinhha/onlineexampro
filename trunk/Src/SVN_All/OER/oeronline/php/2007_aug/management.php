<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Why Wait For Trouble?<br>
</font></b><font face="Verdana" size="1"><b>Performance, accountability and 
satisfaction all need to be aligned to the vision of the business organisation, 
writes Kenneth W. Freeman</b></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="manage.jpg" width="350" height="234"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
For more than 15 years, as a senior executive, a CEO and an external advisor, I 
have led or helped lead many businesses out of crisis and back to financial and 
organisational viability.<br>
<br>
The diagnosis essentially starts and ends with three vital questions:<br>
• Is there evidence of imbalance or excess, either in the company or in the 
industry around it?<br>
• Are the perceptions held by the senior executives of the company aligned or 
contrary?<br>
• What is the level of accountability among the employees?<br>
<br>
<b>Excesses and imbalances</b><br>
Once you start to look for them, imbalances are visible in every turnaround 
situation. Some companies are rife with behavioural excesses; people have 
collectively lost their balance.<br>
<br>
One problem is an excess of leadership, which occurs when a leader is too 
dominant or has been in the job too long. Companies can also get out of balance 
with their portfolio of products or services – either too scattered among many 
offerings or too focussed on a few.<br>
<br>
<b>Aligning perceptions</b><br>
I have observed five distinct stages in any company’s condition. <br>
<br>
<b>Stage 1</b> is “bleeding.” The company is in crisis and its business is 
deteriorating rapidly.<br>
<br>
<b>Stage 2</b> is “stability.” Business, although not great, is not getting any 
worse. In this stage, you can predict what tomorrow is going to look like: just 
like today.<br>
<br>
<b>Stage 3</b> is “gradual improvement.” The company sees small improvements in 
profitability, productivity, customer satisfaction and growth. The sun is rising 
over the lake, and the fish are starting to bite.<br>
<br>
<b>Stage 4</b> is “rapid improvement”: The fish are biting. The company is 
making tremendous sustainable gains. Every company wants to be in this stage and 
the CEOs, featured on the cover of Business Week, usually think they are here – 
even if they’re not.<br>
<br>
Rapid growth can lead to <b>Stage 5</b>, “arrogance.” This stage destroys 
companies. Their leaders have lingered in the sweet spot for so long that they 
believe their own press. This corporate hubris is usually marked by a brief 
plateau, followed by a sharp decline back to Stage One.<br>
<br>
I recently met with the 10 top executives of a struggling company. One person 
said the company was arrogant, three said that it was making rapid improvement, 
three said that it was bleeding, two said that it was stable, and one said that 
it was making gradual improvement. When the answers are all over the map like 
that, the odds are high that the company has a serious and unseen problem.<br>
<b><br>
Performance and satisfaction</b><br>
I also check for alignment in two other areas – performance and what I call 
“satisfaction.” Strong corporate performance is a function of two elements: 
exceptional results, as shown in performance measures, and exemplary behaviour, 
demonstrated by the way people conduct their work and the values instilled in 
the organisation.<br>
<br>
Companies in crisis, as one might expect, tend to have managers who disagree 
about the state of their performance and especially the state of their values. 
Some companies still dispute whether behaviours are important (witness the stock 
option dating scandals), but most companies have learned to recognise the 
importance of strong values in achieving superior performance.<br>
<br>
The second area where I check for alignment is employee and customer 
satisfaction. Companies in turnaround situations usually have focussed almost 
all of their effort on pleasing the shareholder through hitting today’s 
financials at virtually any cost, rather than addressing the inputs that create 
sustainable value.<br>
If your company has engaged and loyal employees, your customers will be engaged 
and loyal as well, and solid financial results will follow. Thus, in addition to 
financial metrics, a CEO’s compensation should be linked to employee and 
customer satisfaction – the metrics that show where leaders can make a genuine 
difference.<br>
<br>
<b>Driving accountability<br>
</b>Within 10 minutes of walking into a factory or a department, it is clear to 
me if there’s an accountability problem. Do the employees make eye contact when 
you walk up to say hello? Do they know the name of the person who’s running the 
factory or their department? If the answer is no, what are the odds that they 
have a clear view of their job and its impact on the customers?<br>
<br>
Once an executive team has gone through the three questions and we have gained 
an understanding of the company’s position, it is time to create a targeted 
improvement plan to focus everyone’s energies. This one-page blueprint includes:<br>
• Company values: behaviours that, once established, will ideally be in place 
forever<br>
• Vision and mission: two simple, memorable sentences that describe, 
respectively, where we expect the company to be in five to 10 years, and in 
three to five years<br>
• A few strategic goals to be achieved in the next couple of years<br>
• Specific actions to achieve each of the strategic goals, with clear metrics 
and accountabilities<br>
<br>
In a turnaround, I create a one-page blueprint with inputs from across the 
company and deploy it to every employee through town meetings, facility visits 
and update calls. Thanks to the blueprint, everyone at every level has a common 
view of the world. They all know what the company stands for, what we are going 
to do and how each of them fits in.<br>
<br>
You need not wait for a turnaround: If you lay out the game plan when your 
company is performing well, then your travels through the business stages to 
rapid improvement may be so brief and mild that you’ll never need someone like 
me to help pull you out of crisis.<br>
<br>
<i>(Kenneth W. Freeman is a Managing Director at Kohlberg Kravis Roberts &amp; Co. He 
is also the founder and former chairman and CEO of Quest Diagnostics Inc.)<br>
