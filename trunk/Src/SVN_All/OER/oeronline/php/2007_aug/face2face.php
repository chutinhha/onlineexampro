<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">‘Not worried about the competition’</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1"><b>Andrew Duff, CEO of National Bank of Oman in a no-holds-barred conversation 
with Ramesh Kumar and Akshay Bhatnagar talks about the transformation in the 
fortune of the bank in his tenure and the radical changes that he is bringing-in 
to sustain and strengthen the recent gains</b><br>
<br>
<b>
<img border="0" src="face1.jpg" width="150" height="170" align="left" hspace="6" vspace="4">You will be completing two years at the helm. When you look back, how does it 
feel? Are you satisfied with your performance?</b><br>
I think we have come a long way. The best indicator is our financial 
performance. If you look at the first half results of NBO this year, you can 
make out the progress that we have achieved. The net profit of RO19.2 million 
reflects robust and continuing evidence of the bank’s successful growth 
strategy. They have increased by 46 per cent as compared to the corresponding 
period in 2006.<br>
<br>
But we can always do things better and faster. If I look back, we initially 
underestimated the transition that was necessary for the bank – from where we 
were and where we wanted to be. The transition became all the more necessary due 
to lack of investments in the period 2002-2005 in every aspect – from systems, 
people, customer management… a big change was needed. I think we didn’t focus 
enough on customer satisfaction per se. We focused a lot more on cleaning up in 
the transition process. Sure, we have improved on the customer front also – our 
figures on growth in customers indicate that – but we need to do a lot more.<br>
<br>
<b>What’s the benchmark for you on the customer satisfaction?<br>
</b>We measure where we are and our benchmark is – constant improvement. Now 
that we have got the bank on track, in the next 36 months we need to focus on 
service and products. For example, we are looking at improving on our 
distribution channels. More branches will be opened, many more ATMs will be 
introduced in the coming months to make us more accessible. We will continue to 
invest in it on a regular basis.<br>
<br>
<b>How many branches and ATMs you have currently?</b><br>
We have 52 branches and 86 ATMs. We are investing in the future. We have 
identified 15 core processes in our organisation which account for 80 per cent 
of our business with the customers. We have recently started a cross-functional 
quality programme in the organisation. The first major project is under way as 
we talk. The objective is that we should improve from 80 per cent mark to 90 per 
cent mark through focusing on those 15 core processes. To put it simply, if 
currently it takes us 10 days to do something we want to do it in one day. If we 
are making 10 mistakes out of 100 actions, we want to reduce it to just one 
mistake in 100. When we improve on the focus in facing the core customers, the 
customers feel it and we move from pure customer service to area of emotions. 
Once we do that we start to really establish a relationship with the customer at 
all levels. It enables us to cross sell products. It also enables to price the 
products at a premium relative to the market place.<br>
<br>
We discuss a lot on how can we move from a pure competitive basis (what we are 
doing is competing on price and similar products) to how can we get our 
customers involved with us. The issue here is that what we haven’t really done 
in the last two years – I think – is that we haven’t focused really on the 
customer. Our customer experience has definitely improved (it shows as the bank 
has improved its performance) but I think we have not taken it to the next 
level.<br>
<br>
<b>How much credit goes to you for this transformation?<br>
</b>About one per cent! This is all about teamwork. For example, all our 
strategies are being developed ‘Bottoms Up’. I’ve been lucky to have an 
experience of seeing and managing few things in other markets that others have 
not experienced here. Cross functionality is not restricted to our customer 
focus alone. I want it in every aspect of our business. I want ‘walls to 
disappear’ in our bank to deliver to our customers.<br>
<br>
<b>Do you bump into ego clashes while following cross-functional concept?</b><br>
Of course!<br>
<br>
<b>How do you manage such challenges?</b><br>
This is one of the cultural changes that the organisation is going through. We 
have developed cross-functional management committees rather than business 
related committees. For example, in a consumer banking sales meeting, there will 
be people from our many other divisions such as corporate banking too. We have 
to get our people not to be defensive. As we establish a process, we get people 
from other businesses to contribute as they don’t have a vested interest and can 
give a fresh perspective. We have to leave egos at home and focus on what is 
important – manage the customer – instead of pursuing personal gratification.
<br>
<br>
I’m not saying that we have eliminated this at NBO completely but if we keep 
going the way we are going, in another couple of years, we will be a fairly turf 
free organisation and customers will feel it. I’m committed to it. I’m 
passionate about ‘getting rid of barriers’. This is part of leadership training 
and development. We are making our 160 people undergo leadership training that 
is slanted towards feedback, coaching, boosting performance rather than 
commanding control. It is all about consensus, listening to people.<br>
<br>
<b>What kind of veto power do you enjoy as the decisions finally reach you at 
the top?</b><br>
I’ve a strong veto power but I exercise it extremely judiciously because if I 
start using it too often we will go back to command and control! It will be 
against what we are trying to achieve in the organisation. My veto is restricted 
only in cases related to management integrity, corporate responsibility and 
credit risk.<br>
<br>
<b>How you are gearing up on the technology front? Any new products or services 
you will introduce that could be enabled with the introduction of new 
technology? <br>
</b>Our corporate Internet banking service – an integrated cash 
management/payment system – should be out in the middle of August. We are in the 
process of selecting a window to implement a new front-end system for the bank 
that will be browser based. It will be integrated into our all distribution 
channels so whether it is the call centre, branches, ATMs… wherever customers 
will touch base with us one system will be there. For example, if a customer 
walks into a branch, the customer service person will know what the customer 
talked about yesterday with the call centre. So it will have a repository of 
customers’ information. So this new technology will enable us for a better 
customer service delivery. But to put this kind of a system in place throughout 
the country we need higher bandwidth. Omantel is working on it. I see it 
becoming a reality in the next 12 months. We expect to be the first player to do 
so in Oman.<br>
<br>
We are also introducing payment (salary) cards shortly. It is interconnected 
with corporate Internet banking.<br>
<br>
<b>What kind of pressure do you face with a large player at the top of the table 
and new banks joining in the fray pushing you from both the sides?</b><br>
If we focus on the competition we might be focusing on the wrong strategy. We 
have a right strategy, which is devised by us, and we believe it will create the 
desired results for us. We are busy implementing this strategy. Yes, we do look 
at the competition but we don’t worry about them and never try to copy them. The 
only issue we are focusing on with respect to competitors in the current market 
is human resources. The shortage of talented banking professionals in the region 
with new banks and NBFCs coming up is a gulf wide problem. People are fairly 
mobile and it puts pressure on the organisations significantly. But it is not 
having a major impact on us.<br>
<br>
<b>With the pressure to retain and attract new talent, does your bottomlines are 
getting affected? What kind of salary increase has been done in NBO in the 
recent times?</b><br>
Yes, it does create pressure on the bottom lines but it is under control. In 
terms of compensation, we follow a system of pay per performance system. Our 
salary levels are competitive in the market. We are getting more sophisticated 
in terms of measuring employees’ performances. Last year, the bonus paid was the 
highest ever in the history <br>
of the bank.<br>
<br>
<b>What is your opinion on the regulatory mechanism in Oman and GCC vis-à-vis 
global scenario? Do you face any challenge on this front?</b><br>
If you ask a banker anywhere, the answer will be that we don’t want any 
regulation. But history across the world indicates that regulation in banking is 
needed to protect the depositor’s interest and everyone looks at the government 
to ensure this. I think there is definitely a need for regulatory intervention 
or supervision in Oman. Most of the countries in GCC follow almost a similar 
regulatory mechanism which is not very distinct from markets such as Singapore 
or Malaysia. There is a higher degree of self-regulation in markets such as US, 
UK or Australia. The basic intent remains same – protect depositor’s interest. 
The implications are same. The basic issue is how they are implemented.<br>
<br>
The regulators in GCC, Malaysia and Singapore tend to be quite prescriptive. In 
Oman, the current regulatory approach is largely bordered around the Basel II 
which is to improve the management in the banks. I think the Central Bank of 
Oman is making banks move more towards Basel II. If you are a banker, whether it 
is your regulator putting in the rule or you are applying the rule yourself, the 
outcome is same. I don’t think I’ve more pressure than anywhere else I’ve worked 
in. There are certain things I dislike intently but I can understand the 
positive intent behind them.<br>
<br>
<b>Is there any informal club of top bankers in Oman?</b><br>
No. Generally, there is an association of bankers in many markets where the 
association has a direct line to the regulator. It is consulted and can 
influence regulatory policy. Here we have a subcommittee in the Oman Chamber of 
Commerce &amp; Industry (OCCI) but it doesn’t have really that sort of capacity for 
interaction. In the absence of a proper banker’s association, the banks really 
don’t collaborate at all.<br>
<br>
<b>Don’t you think it is necessary in Oman?</b><br>
I would like to have a banker’s association definitely. But there is no point in 
having a banker’s association unless the charter of the constitution of the 
association is such that there is a formal dialogue process with the principle 
regulators. If there isn’t a mechanism for that formal dialogue or consultation 
to take place, then it will be just a gossip club; no point in having it.<br>
<br>
<b>So how banks communicate with the regulator? Do you follow OCCI route or move 
individually?</b><br>
We interact individually with the regulator. CBO holds an annual CEO meeting 
where we discuss number of issues generally. There is an agenda and CBO consults 
the banks accordingly. The meetings are very frank and open and often lead to a 
very wide range of opinions. But if we could have more of them it will be 
better. More will come out of it.<br>
<br>
<b>How you are gearing up to meet the challenges from the labour union related 
act that is leading to formation of labour unions in many companies in Oman?</b><br>
We already have a staff union in place for the last two years. We are the first 
one to do it in banking sector in Oman. We have a very open and transparent 
policy as the union shares its thoughts with the management. The union 
recommendations have led us to improvise our policies.<br>
<br>
<b>Are you prepared to where it is going to lead to in the future?</b><br>
Yes, we do. At the moment, it is a kind of staff grievance avenue. But as we 
have made the organisation far more transparent, the grievances are surfacing 
directly instead of the union route. The next step is where the union goes from 
here.<br>
<br>
<b>In many markets, the union representatives are also on the board of the 
companies. Is it there in NBO?</b><br>
No. I don’t see it happening in the foreseeable future. <b>
