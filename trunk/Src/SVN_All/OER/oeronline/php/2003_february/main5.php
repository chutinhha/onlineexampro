            <td width="70%" bgcolor="#FFFFFF"><font face="Verdana" size="4" color="#FF0000">All
              wheel, alright&nbsp;</font><p><font face="Verdana" size="1"><b>As
              the Subaru Impreza 2003 rolls into the sultanate, </b><b><i>OER</i></b><b>
              reviews the car's latest innovations</b></font></p>
              <p><font face="Verdana" size="2">First impressions of the new Subaru Impreza are - better than expected. It's actually got a lot going for it, particularly when considering its all wheel drive capability and all round sleeker looks. The latest eye candy to enter the local automotive market was launched here at the end of last month. OER took the latest 1.6 litre model for a spin around Muscat last month to ascertain how much of a success the car would be bearing in mind that Japanese car-maker Subaru has in the past been synonymous with control, safety and reliability among its fans. The updated Impreza had a few pleasant surprises.&nbsp;<br>
              <b> Outdoors</b><br>
              A couple of bold changes have been made to Impreza's exterior, changes that have been made to help it get closer to Subaru's design DNA, according to Fuji Heavy Industries - the power behind the brand. The most obvious revisions in comparison to last year's model are its brighter and broader headlamps below a smoother looking bonnet. The wheel arches around the front wheels have become more pronounced too, bulging slightly in an alluring manner. The changes provide more fun and depth to the car's overall elegant features. Otherwise, the Impreza remains mostly unchanged and its slight growth is definitely not noticeable to the eye. For the record, the sedan has become 10 mm longer and 10 mm wider.&nbsp;<br>
              <b> Indoors&nbsp;</b><br>
 Not much has changed inside the car. The interior remains functional and you are offered your fair share of creature comforts. You get keyless entry as standard along with power steering and windows, central door locking, and an automatic air conditioner. Other convenient features include a cup holder in the instrument panel and electric remote control door mirrors. The dashboard, titanium-colour centre console and air-vent surrounds are modern looking and the fitted fine-weave upholstery offers a sportier feel than some other sedans in its class. The tilt adjustable steering wheel is user friendly, for a change. Indeed, the driving position is excellent, and the seats (which have had their height-adjustable mechanism enhanced for easier setting) hint at the racing pedigree for which Subaru is famous. The WRX - Subaru's flagship turbocharged Impreza model that has been the winning World Rally Championship car three times - has been scoring maximum points for handling, steering, and braking for years.&nbsp;<br>
              <b> Performance&nbsp;</b><br>
 The changes go beyond a tweaked exterior. The Impreza has become significantly better at conserving energy. Fuel consumption in the new model has declined to 10.9 litres per 100 km in urban areas from 12.1 litres per 100 km in last year's model.&nbsp;<br>
 The car's acceleration is, however, a little disappointing for the power hungry considering that its super quick sister, the 2.0-litre WRX, will dash 0-100 km/h in 5.7 seconds. OER could not obtain data for the 1.6-litre 16-valve four-cylinder horizontally opposed engine but was informed that it delivers a fair 95 HP at 5,200 rpm. The torque delivered is a moderate 143 Nm at 3,600 rpm. You may not raise many eyebrows at the traffic lights should you wish to pull off rapidly but the power is more than sufficient for the driving conditions in the Sultanate.&nbsp;<br>
              <b> Safety&nbsp;</b><br>
 Impreza is already a much-awarded car for its engineering and safety. The all wheel drive (AWD) system is an outstanding feature of this car. Some may equate the sensation to driving along with a team of telepathic rail engineers, laying the tracks a few feet away from your bonnet. It certainly gives better power distribution, tyre grip and more control as a result - even at high speeds. Active safety is enhanced by the Horizontally Opposed Engine, found only in cars like Porsche, is ideal for an all wheel drive application that gives increased stability at sharp corners, in windy conditions and on slippery surfaces.&nbsp;<br>
              <b> Price&nbsp;<br>
              </b> In Oman, the Subaru Impreza is yours for a sensational starting price tag of RO4,500 (US$11,700) for the 1.6 litre. In terms of pricing and model type, the Toyota Corolla, Nissan Sunny, Mitsubishi Lancer, and Volkswagen Polo are all fairly close competitors. Among these, the Impreza is worth a closer look.&nbsp;</font></p>
