            <td width="70%" bgcolor="#FFFFFF"><font face="Verdana" size="4" color="#FF0000">Home
              truths</font><p><b><font face="Verdana" size="1">Buy-to-let has
              been a safe investment in the past, providing a good monthly
              income for Omani entrepreneurs and small companies. OER provides a
              report on the prevailing trends in the local property rental
              market</font></b></p>
                    <p><font size="2" face="Verdana">Activity is relatively high
                    in Muscat's property rental market with both landlords and
                    tenants often surprised by the pace at which a good property
                    is let. The past year has been a roller-coaster ride for
                    this sector that started on a quiet note but gathered pace
                    since the summer months of 2002. In Oman, incoming
                    expatriates drive the market and capital values to a large
                    extent, particularly in attractive areas like Shatti Al
                    Qurum. Although the amount of space is increasing, the
                    overall number of tenants has not risen significantly, thus
                    leaving the market fairly flat.&nbsp;<br>
                    <img border="0" src="Property.jpg" align="left" hspace="5" vspace="5" width="150" height="200">&quot;There
                    is consistent talk in Oman about people leaving the country
                    but there is less said about those arriving into town. In
                    the upper end of the property rental market, Muscat has seen
                    large number of arrivals towards the end of last year. This
                    trend continued in January 2003,&quot;says Christopher
                    Steel, general manager of Hamptons International (Oman).<br>
                    Although the rate of Omanisation in the workplace is
                    increasing, a lot of these arrivals are replacing expatriate
                    workers who have left the country, says Steel who
                    acknowledges that there are genuine new foreign additions
                    coming in. &quot;Put simply, it's not that the overall ratio
                    of foreigners has gone up but that evidently more job
                    opportunities are being created in the economy.&nbsp;<br>
                    &quot;There has been a noticeable demographic change due to
                    the new arrivals who have entered the mid-to-high end of the
                    market. We have seen a shift towards more singles, young
                    couples and older couples rather than middle-aged couples
                    coming into the Gulf,&quot; he adds. The main reason for
                    this could be that salaries in Europe and the US have been
                    steadily rising leaving fewer reasons for expatriates from
                    these areas of the world to work in the Gulf. Also, a lot of
                    families with young children probably don't necessarily feel
                    secure in the Gulf in the current geo-political
                    climate.&nbsp;<br>
                    The two-tier market dividing sought after properties that
                    meet tenancy specifications and poorly looked after
                    buildings which tend to lie idle has been accentuated
                    further in the past year. &quot;If landlords don't upgrade
                    their properties, they cannot lease and property agents will
                    not take them on their books,&quot; confirms Steel.&nbsp;<br>
                    Since February 2002, rents have remained static. In the past
                    three years, there has been no significant fall in rents
                    across Muscat. &quot;There has been no tangible change in
                    rental levels in Muscat across the board but we have seen
                    strengthening of rents in Al Ghubrah and Al Athaiba,&quot;
                    adds Steel. The availability of new accommodation, proximity
                    to good beaches and the ever-expanding facilities in these
                    areas has made them the 'hot spots' of Muscat. New or
                    well-refurbished property in these areas tends to lease
                    'before it comes to market' reflecting the appeal these
                    areas have.&nbsp;</font></p>
                    <p align="center"><img border="0" src="PROPERTY-GRAPH.jpg" width="400" height="277"><br>
                    <font face="Verdana" size="1">*Rents per month (in RO) based
                    on 3 bedroom apartment or villa</font></p>
                    <p><font size="2" face="Verdana">There has been a slow
                    rental property turnover for larger villas in Shatti Al
                    Qurum (reflected by increasing trend for owner occupation).
                    Ruwi, Al Wadi Al Kabir and Wattaya are the preferred choice
                    locations for apartment dwellers in the south of Muscat.
                    With less new stock available in the market, Al Khuwair is
                    gaining popularity as an alternative with new apartments
                    leasing quickly at or very close to asking rents.&nbsp;<br>
                    By and large, the gap between landlord expectations and
                    tenants willingness to pay has narrowed slightly. There is
                    realism in the market that the rents being charged are in
                    line with the demand.&nbsp;<br>
                    Michael Lowes, general manager of Cluttons (Oman), says,
                    &quot;There isn't any substantial new demand in the market.
                    Basically, people are moving from one place to another. It's
                    still a tenant's market. The supply is much greater than
                    demand, so the tenant is in a very good position to
                    negotiate and can often move into a better building without
                    having to worry about an increase in rent.&quot;<br>
                    &quot;There are some exciting property development schemes
                    being developed. But if someone proposes to build another
                    150 residential units, where will the people come from to
                    occupy them?&quot; he asks. &quot;Chances are that they are
                    not new arrivals but simply vacating an existing dwelling in
                    the city&quot;<br>
                    <b>Runaway success&nbsp;</b><br>
                    W.J. Towell &amp; Co. LLC has developed a residential
                    accommodation block in Medinat Al Sultan Qaboos that
                    contains 49 units consisting of two-bedroom and
                    three-bedroom apartments as well as penthouse suites. The
                    block, which boasts split-level marble flooring, already has
                    over 60 per cent bookings and occupancy. The rent is between
                    RO300 and RO500 per month. Amenities include a swimming
                    pool, 24-hour security provided by a watchman and an
                    intercom.<br>
                    &quot;The WJ Towell apartments in MQ have been very
                    successful,&quot; notes one property agent. &quot;Its
                    location is pivotal to its success and many people have paid
                    significant premiums to reside there. &quot;<br>
                    <b>Building an Oasis&nbsp;</b><br>
                    <img border="0" src="property-Michael.jpg" align="right" hspace="5" vspace="5" width="135" height="190">Muscat
                    Oasis, is another noteworthy residential and commercial
                    complex. Currently under construction, the US$30 million
                    project headed by Sheikh Salim Nasser Alssiyabi is aiming to
                    provide 400 two bedroom and three bedroom luxury apartments
                    by November 2005. The first phase of the project, a block of
                    eight buildings, is slated for occupancy by November 2003.
                    The amenities planned include two swimming pools, a
                    children's outdoor play area, a gymnasium and sports/social
                    hall, a tennis and basketball court, a family supermarket, a
                    pharmacy, a clinic, and a 24-hour creche and nursery.&nbsp;<br>
                    &quot;It's the largest project that the company has
                    undertaken,&quot; says Iqbal Jaferi, general manager of
                    Allied Corporation LLC, the parent group company of Allied
                    Real Estate which is managing Muscat Oasis.&nbsp;<br>
                    &quot;Our feasibility study indicates that consumers want to
                    live where they have more services on their doorstep. We
                    will be providing areas for children to play, and for
                    grown-ups to socialise, shop and stay fit. The tenants will
                    be secure, comfortable and have all the basic services along
                    with special catering, housekeeping and accident emergency
                    provision if needed,&quot; he adds.&nbsp;<br>
                    &quot;The apartments at Muscat Oasis may not be very
                    different from what's available in the market, but the
                    development will do very well on the basis of the added
                    services it is offering,&quot; agrees Steel of Hamptons
                    International.</font></p>
