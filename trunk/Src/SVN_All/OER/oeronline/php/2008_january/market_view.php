</font><font face="Verdana"><b><font size="4">Busy month on MSM</font></b><font size="1"><br>
<b>Up another 10 per cent last month, the MSM still stands ahead of all other 
GCC and MENA markets<br>
</b><br>
Another historic high was created on the Muscat Securities Market (MSM) during 
the one-month period ended December 15, 2007, with the index going past the 
9,000-points mark. The benchmark MSM-30 general index, which has generated an 
59.4 per cent year to date return, zoomed by 10 per cent or 813 points during 
the period, making it yet another busy month for the market intermediaries. Once 
again, the banking sector took the lead and pushed the index up (this sector has 
over 45 per cent weightage in the index), itself recording a 12.4 per cent jump. 
This was followed by a 10.3 per cent rise in the industry index and a 5.8 per 
cent increase in the services index. The market turnover too saw a substantial 
increase at RO370 million; average daily turnover exceeding RO16 million during 
the month.<br>
<br>
Year to Date (YTD), MSM still stands ahead of all other GCC and MENA markets, 
with Abu Dhabi bourse being the closest contender, recording a 51 per cent 
increase. It is evident now that institutions from foreign locations have 
started accumulating shares in Oman and their operations are adding to the local 
liquidity, firing up the market in the process.<br>
<br>
In key corporate events, Takamul Investment Company, which is under formation, 
and Oman Cables Industry SAOG (OCI) entered into an agreement to put up a 48,000 
MT per annum capacity aluminium rod and electrical conductor manufacturing plant 
in Sohar. The project owned 51 per cent by OCI and 49 per cent by Takamul, is 
expected to be operational in the first quarter of 2009. It is the second 
project to be launched in the aluminium downstream park being developed adjacent 
to the smelter. Oman Cables’ shares saw huge demand from investors following the 
announcement. In another interesting development, the board of directors of 
National Finance Company SAOG met on December 3, 2007, and discussed the 
possibility of a merger between NFC and Muscat Finance Company Ltd SAOG. 
According to the NFC board, a merged company would have a higher market share 
and be in a better position to consider regional expansion. <br>
<br>
Al Anwar Holding remained in limelight due to the news that it plans to list its 
subsidiary, Voltamp Manufacturing LLC. The shareholders of Voltamp Manufacturing 
Co. LLC in their meeting held on December 10, 2007, granted approval to go ahead 
with the floatation of its shares through an IPO, which is to be lead managed by 
Oman Arab Bank.</font></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
