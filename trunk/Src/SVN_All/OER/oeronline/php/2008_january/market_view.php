<font size="1" face="Verdana"><b>GCC Market Commentary</b><br>
The markets ended mixed, with most of them ending in the positive territory for 
the one month period ending December 15. The Saudi exchange was the best 
performer and ended 17.96 per cent higher. All the broader indices on the bourse 
performed well, with the banking sector taking the lead and generating a return 
of 27.21 per cent, followed by cement and services stocks, generating a return 
of 18.51 per cent and 14.44 per cent, respectively. The Abu Dhabi and Dubai 
bourses too closed in green, generating a return of 7.60 per cent and 2.28 per 
cent respectively. While the real estate index was the ace performer on the Abu 
Dhabi bourse ending up 13.34 per cent, the insurance stocks were among the 
toppers on the Dubai exchange closing 4.94 per cent above month ago levels. The 
Bahrain general index was almost flat and closed 0.37 per cent above month ago 
levels. The Qatari and Kuwaiti indices occupied the losers’ list during the 
month, shedding 1.64 per cent and 1.96 per cent, respectively. There were mixed 
results on the Doha exchange, with the insurance sector being among the top 
gainers, closing up by 2.33 per cent. However, the Kuwait bourse witnessed 
losses in all the broader indices. </font>
