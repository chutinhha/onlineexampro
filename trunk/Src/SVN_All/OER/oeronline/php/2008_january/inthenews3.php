</font><b><font face="Verdana" size="4">
Hedge funds take centrestage <br>
</font></b><font face="Verdana" size="1">
<b>Man Investments is optimistic in its outlook report for 2008 and says the 
uncertainty over the US economy is good for hedge funds <br>
</b><br>
<img border="0" src="news4.jpg" width="200" height="300" align="left" hspace="8" vspace="6">Man Investments, one of the world’s largest suppliers of hedge fund products 
with an estimated US$70 billion under management, feels the uncertainty over the 
US economy is likely to create substantial trading opportunities for hedge funds 
in 2008 but possible market disruptions could affect some strategies. In its 
annual review and preview report on the hedge fund industry, Man Investments 
gives a mainly upbeat outlook for the industry in 2008. <br>
<br>
The firm’s team of research economists predicts that hedge fund strategies 
likely to prosper next year will include relative value, event driven and 
managed futures.<br>
<br>
Antoine Massad, Chief Executive Officer, Man Investments Middle East says, “The 
big challenge for hedge funds next year will be to adapt to the different 
possible macroeconomic scenarios that will materialise. Volatility will continue 
to be the key factor in determining how vigorously hedge funds perform.” <br>
<br>
Thomas Della Casa, Head of the Research, Strategy and Analysis Group at Man 
Investments and co-author of the report, with analyst Mark Rechsteiner, adds: 
“Volatility arbitrageurs will most likely benefit from a general long exposure 
to various levels of volatility and frequently shifting sentiment. At the same 
time, fixed income managers will look for opportunities arising from uncertainty 
caused by central bank actions.” <br>
<br>
For the event-driven hedge fund style, the authors expect continued strength 
despite the recent slowdown in the number of mergers and acquisitions. They say 
many companies with large amounts of cash on their balance sheets will be 
looking to external growth. In addition, managed futures strategies will benefit 
from difficult market conditions since these have, historically, created strong 
trends, which directional programmes can exploit. <br>
<br>
<b>Performance cushion</b><br>
The report observes that hedge funds started 2007 well and were able largely to 
navigate around the spring turbulence. In the period leading to the subprime 
crisis, most hedge funds had already built up a performance cushion and many 
managers expressed cautious views on the markets. Quantitative equity 
strategies, however, suffered losses during the summer turmoil due to unusual 
market behaviour, which their models could not replicate. <br>
<br>
Summarising the summer events, Della Casa says: “August was difficult for all 
hedge fund styles, with average downturns of at least 2 per cent but, in many 
cases, much higher. <br>
<br>
“However, the following two months turned out to be very strong for diversified 
hedge funds, yielding returns of about 5 to 6 per cent that more than 
compensated for the August falls. Even though, on early figures, November 
appears to have taken back some of these gains, given an average December, the 
hedge fund industry is set to post its strongest year since 2003, with most 
funds exceeding performance targets.” <br>
<br>
Assuming a future slowdown of the global economy caused by effects from the US 
housing market, rising energy costs and stricter credit availability, the report 
says: “While market tailwinds are expected to abate, the alpha generating skills 
of hedge fund managers will be essential to meet investor expectations. In such 
an environment, we would expect heightened volatility with clear sector and 
market differentiation. <br>
<br>
“Those managers who will be unable to achieve set target returns and who lack 
the necessary risk management processes will find it hard to survive. This will 
further intensify the consolidation process in the industry.”
