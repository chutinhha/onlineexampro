        <b>Retail banks fail to make the most of sales opportunities </b><br>
        A new Booz Allen study “Striving for growth: Best practices in retail 
        banking sales and service channels” has revealed that the majority of 
        banks are failing to fully meet customer needs and maximize sales 
        channel performance. The comprehensive study, which used customer 
        research and mystery shopping to assess 100 banks across 17 countries 
        (including 12 banks in the GCC), looked at what customers want from 
        their banks, how banks are performing and best practices to drive 
        profitable growth. Based on the findings of this research, the Booz 
        Allen Revenue Enhancement (BRE) Index was developed, taking into account 
        performance across branches, call centers, online, mobile sales forces 
        and a multi-channel index. <br>
        <br>
        Despite technological advances, globally customers still prefer banking 
        in branches – 63 per cent choose to use a branch for purchasing current 
        and savings account products and 54 per cent choose their branch to 
        purchase mortgages. The preferences of GCC customers are almost 
        identical to the global average. However, branch importance is in 
        relative decline. <br>
        <br>
        For purchasing less complex products, such as credit cards, globally 
        over half would now use an alternative channel with 21 per cent 
        purchasing online and 16 per cent by telephone. The situation is 
        inverted in the GCC where 18 per cent of customers prefer purchasing by 
        telephone compared to only 11 per cent who preferred purchasing online. 
        Globally, online channel usage is rising and is preferred over phone, 
        particularly for simple products and transactions; however, this is 
        taking place at a slower pace in the GCC. The trend away from the branch 
        is being led by affluent consumers, who tend to be time poor and are 30 
        per cent more likely to prefer using online services. <br>
        <br>
        “The branch is declining in importance, but banks are failing to 
        capitalize on the enormous potential of key growth channels such as 
        online and mobile sales forces, because the performance of these 
        channels does not meet customer needs and expectations,” said London 
        based Booz Allen vice president Alan Gemes. <br>
        <br>
        Across all channels there is a significant difference between the 
        performance of the best banks, which show near excellent service, and 
        the rest of the pack. The top performing GCC banks achieved the highest 
        scores at near the global average, across most channels; whereas, the 
        remaining banks had a wide performance variation across the various 
        channels. </font></p>
