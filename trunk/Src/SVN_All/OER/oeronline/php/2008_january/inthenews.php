</font><b><font face="Verdana" size="4">
Ready to punch with extraa force <br>
</font></b><font face="Verdana" size="1">
<b>United Media Services, Oman’s premier integrated media and communications 
group, is poised for an exciting expansion phase as it celebrates 20-years of 
successful operations<br>
</b><br>
<img border="0" src="news.jpg" width="250" height="242" align="left" hspace="8" vspace="6">With the beginning of 2008, United Media Services is set for more excitement 
ahead. An occasion to celebrate 20-years of successful and sustainable business 
operations. New owners to drive the company towards the path of becoming a 
leading pan-GCC player. <br>
<br>
From a modest beginning in 1988, United Media Services has come a long way to 
emerge as Oman’s only fully integrated media and communications’ group. Its 
portfolio of services and products spans over publishing, advertising, digital 
solutions, events, public relations, media representation, and media 
distribution &amp; communications. <br>
<br>
Talking about the high points of the group, Sandeep Sehgal, general manager of 
UMS says, “Over the last two decades, we have successfully added and developed 
multiple competencies to evolve as the most preferred and unique solutions’ 
provider in the media and communication business. On a sustainable basis, we 
have continued to grow, added to the shareholder’s value and generated profits.”<br>
<br>
Despite operating in an environment that is beset with increased competition and 
volatile employment market, UMS has managed to identify and build on the 
existing business opportunities not just in Oman but other markets in the GCC as 
well. Sandeep says, “People are our principal assets. We have retained our core 
and high performing people to ensure stability of our business management. Our 
clients have continued to repose their faith in us and have been the strategic 
partners in our success story. As a result, we have not only grown significantly 
in Oman but crossed border also. Our successful operations in Qatar is a solid 
testimony of our overseas expansion. We are the only media company in Oman that 
has ventured out in other GCC markets.” <br>
<br>
UMS has a portfolio of premium brands which are leaders in their segments. OER 
is a case in point. Sandeep states: “With UMS enjoying a strong brand equity of 
its own, we have successfully created and built what we call ‘wealth brands’. 
With the stamp of UMS quality seal, our array of brands are highly respected in 
the market for the value they bring in. Each of them have their own IPRs and add 
to the sustainable profits.” <br>
<br>
The success of UMS brands is reflected in the strong financial performance of 
the group. The top line has grown by almost 60 per cent in 2006 compared to 2003 
figures. The bottom line progress is even more impressive over the same period. 
Sandeep informs that the company will be closing 2007 with yet another 
impressive growth rate. <br>
<br>
UMS has begun the new year under a new ownership. Given UMS’ market leadership 
and outstanding growth record, what led Renaissance Services SAOG, UMS’ 
erstwhile holding company, to divest the business? Samir J. Fancy, chairman of 
Renaissance, had stated categorically in the company’s annual report 2006 that 
“from 2007, we (Renaissance) shall also consider divestment of some 
high-performing assets that are not strictly aligned with our core strategic 
focus (oil &amp; gas sector), and we shall be looking for buyers who fully 
appreciate the value of these assets and are able to realize their full economic 
potential going forward.” <br>
<br>
In a recent statement, confirming Fancy’s proclamation, Stephen R Thomas CEO of 
Renaissance Services, said: “We have previously advised our intention to divest 
non-core assets that do not fit directly in the Renaissance oil &amp; gas services 
growth strategy. This included consideration to divest our high-performance, 
high potential media business. We said we would consider divestment of this 
asset if the price offers realization of value for shareholders; and if the 
investment strategy or industry focus of the buyer, offers a platform for UMS to 
meet its ongoing growth potential to the maximum advantage of UMS employees and 
UMS customers. These criteria have been met with this transaction, and we have 
no doubt that UMS will continue on its journey in good hands at both the 
ownership and employee level.” <br>
<br>
A consortium of Omani-led strategic investors have acquired UMS. The 
spokesperson on behalf of the consortium informed, “The media and communication 
business in the Middle East is seeing unprecedented growth. With its robust 
economy and focus on developing the non-oil sector, Oman’s media and 
communication platforms too are buoyant about greater support from other 
sectors. This makes a strong business case for M&amp;C as a rapid, high potential 
and high ROI sector. We strongly believe that UMS is a prime asset and have 
invested to acquire its ownership. The company has been consistently delivering 
high growth and has built a strong goodwill in the market”. It is not often that 
a business with such strong performance and clear potential comes on the market. 
For strategic reason, UMS became available for investment and the investors’ 
have seized the opportunity with open arms. <br>
<br>
Talking about the ownership transition and its impact on the future of UMS, 
Sandeep states: “As part of high corporate governance standards, the management 
of UMS was integrally involved in the acquisition process to ensure that the new 
environment leads to growth of the business as per the stated objectives. Just 
as Renaissance has been an excellent parent to UMS, we look forward to further 
progress and development under the new environment. The management team at UMS, 
including myself, remains in place to ensure direct relationships are unchanged. 
At UMS, we are seizing the opportunity to scale up and fast track our growth 
initiatives. We would also employ the new resources now available to support 
this exponential growth plan and a regional footprint for the company.” <br>
<br>
Elaborating on the growth plans, Sandeep says, “We have a strong base in Oman. 
As Omani market has a limited size, the way to grow is to spread footprints to 
other markets in the GCC region. We already have full-fledged operation in Qatar 
that has done exceedingly well. We have plans in place to replicate in part or 
fully our successful integrated business model to rest of the GCC states.” <br>
<br>
What does it entail for UMS’ Oman operations? Sandeep replies, “Definitely, Oman 
is our stronghold. We will scale up our existing offerings and are actively 
pursuing new business opportunities to further broadbase our product portfolio. 
I see tremendous business potential in areas including market research, digital 
marketing, new direct marketing services and below-the-line (BTL) services. With 
Oman’s economy in top gear, the potential to grow further is immense.” <br>
<br>
The man in a hurry, along with his team, is all set to write a new chapter in 
the media business in not just Oman but GCC region as well!</font></p><div align="center">
