<br>
<font face="Verdana">Google to launch Wikipedia rival</font></b></font><font face="Verdana" size="1"><br>
<img border="0" src="world2.jpg" width="175" height="117" align="right" hspace="8" vspace="6">The search giant Google’s ‘knol’ project will offer encyclopaedia entries with 
bylines and advertisements. Google is working on a collaborative online 
encyclopaedia that could compete with Wikipedia, the popular user-edited 
encyclopaedia. The “knol” project – or a unit of knowledge in keeping with 
Google’s creative shorthand – will allow a user to create an entry on virtually 
any topic. Like Wikipedia, it will allow anyone to add an entry. But unlike 
anonymous postings on Wikipedia, it will post an author’s byline and profile 
with each entry. Another difference from Wikipedia is that readers will not be 
able to edit knol entries. Instead, they can rank them, with their rankings 
being interpreted by Google’s search engine in any search operation. Wikipedians, 
for their part, don’t seem to be losing any sleep over Google’s project. In an 
emailed statement, Wikipedia said it welcomed the initiative. “The more good 
free content,” it said, “the better for the world.” <br>
