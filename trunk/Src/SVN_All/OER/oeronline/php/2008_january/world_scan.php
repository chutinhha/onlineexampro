<font face="Verdana">WOM marketing spending to top US$1bn in ‘07</font></b></font><font face="Verdana" size="1"><br>
<img border="0" src="world3.jpg" width="175" height="122" align="left" hspace="8" vspace="6">Word-of-mouth (WOM) marketing spend increased 35.9 per cent in 2006 to $981 
million (from US$722 million in 2005) and is easily expected to top US$1 billion 
in 2007, making it one of the fastest-growing alternative media segments, 
reveals PQ Media’s Word-of-Mouth Marketing Forecast 2006-2011, According to its 
sister site, MarketingCharts, The growth driver is the continued consumer shift 
to alternative media and marketers’ need for increased brand engagement and 
return on investment. WOM marketing grew almost five times faster than the 
overall marketing services sector in 2006 (35. per cent vs. 7.7 per cent), and 
more than six times faster than the overall media industry growth and nominal 
GDP. WOM marketing spending rose at a compound annual growth rate of 66.7 per 
cent from 2001 to 2006–from US$76 million to US$981 million. Spending on WOM 
marketing is forecast to grow 37.7 per cent in 2007 to US$1.35 billion, as brand 
marketers continue to shift more dollars to WOM marketing tactics as part of 
their cross-platform marketing campaigns. Total WOM marketing expenditures are 
projected to climb at a CAGR of 30.4 per cent to $3.70 billion in the 2006-2011 
period. <br>
