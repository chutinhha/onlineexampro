</font><font size="2" color="#CC3300"><b><font face="Verdana">Eco-friendly Bamboo PC</font><br>
</b></font><font face="Verdana" size="1">
<img border="0" src="world4.jpg" width="123" height="83" align="right" hspace="8" vspace="6">Harvesting bambooan abundant, flexible, 
durable and fast-growing grass, and using it to make notebooks was certainly not 
on the minds of Steve Jobs and Steve Woznaik when they built the Apple I, an 
early personal computer that consisted of a circuit board in a simple wooden 
box, in 1976. While their product allowed computer makers the world over to make 
advanced PCs in metal and plastic casings, Taiwan’s Asustek Computer Inc sees 
its success in bamboo. Its eco-friendly notebook PC is encased in bamboo strips 
available in different shades. The Asus Eco Book, as it is known, is unlikely to 
harm the environment as processing wood from trees might. Asustek says the 
product is still in the prototype stage and engineers are checking to see if 
bamboo is suitable for laptops, which have to endure extreme conditions while 
allowing heat from microprocessors and monitors to escape. </font></p>
