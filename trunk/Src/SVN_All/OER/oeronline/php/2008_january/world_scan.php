</font><font size="2" color="#CC3300" face="Verdana"><b>World’s first ship tunnel to come up in 
Norway</b></font><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300"><b>
<img border="0" src="world.jpg" width="200" height="104" align="right" hspace="6" vspace="4"></b></font><font face="Verdana" size="1">Norway is planning to build the world’s first shipping tunnel, which will save 
time and money for vessels passing through a coastal area known for its 
dangerous seas. Strong winds, high waves and powerful currents in the area of 
Stad, on the southwest coast of Norway, cause long delays while ships wait for 
calmer conditions. A recent report from the Norwegian Coastal Administration 
recommended building the 1,700-metre (5,577 feet) tunnel and concluded that it 
would be cost effective. The tunnel, estimated to cost around US$310 million and 
which will take five years to build, will cut through a peninsula, saving ships 
the risky journey around the coastline. <br>
