<font face="Verdana">KPMG becomes US$20bn firm</font></b></font><font face="Verdana" size="1"><br>
<img border="0" src="world1.jpg" width="80" height="104" align="left" hspace="8" vspace="6">KPMG, the global network of professional service firms providing audit, tax and 
advisory services, has reported combined revenues of US$19.8 billion for the 
fiscal year ending September 30, 2007, versus US$16.9 billion a year earlier, 
reflecting double-digit growth across all service lines. KPMG’s advisory 
services experienced growth in all regions and grew fastest at 22.2 per cent to 
US$6.43 billion, fuelled by increased M&amp;A activity, heightened regulations and 
demand for performance improvement. Revenue from the Asia Pacific region grew 
21.6 per cent to US$2.55 billion in FY07, driven by strong performance across 
all practice areas, with both Japan and China showing particularly strong 
growth. Exceptional performance was also evident in the BRIC countries (Brazil, 
Russia, India and China), with aggregate revenues increasing by 41 per cent in 
the past year. In the Asia Pacific region, the influence of private equity 
funds, strong overall M&amp;A activity and the maturation of the capital markets 
provided robust opportunities for all of KPMG’s services. Multinational 
companies are capitalising on the investment in the booming domestic economies 
of China and Vietnam. Also, back office and IT outsourcing operations are 
expanding in Malaysia and the Philippines. <br>
