</font><b><font face="Verdana" size="4">The Blue City takes off </font></b><font face="Verdana" size="1">
<br>
<b>With the ground breaking of phase I of Al Madina A’Zarqa (The Blue City) at 
Al Sawadi, Oman’s tourism landscape is set for a major change <br>
<br>
<img border="0" src="news2.jpg" width="350" height="214" align="left" hspace="8" vspace="6"></b>The development of the Blue City, the US$20-billion real estate project, has 
moved into top gear, with a glittering ground breaking ceremony held in 
December. The event also saw the first phase of Al Madina A’Zarqa, featuring a 
select group of properties that will form part of the ‘town within the city’ – 
and which will be ready for delivery by 2010, being put up for sale. Further 
properties will be released for sale in the coming months. <br>
<br>
His Highness Sayyid Haitham Bin Tariq Al Said reiterated: “Al Madina A’Zarqa is 
planned to become a model for a lifestyle that adapts to the surrounding 
environment. The project has not excluded traditional Omani architectural design 
and construction; on the contrary, it is inspired by architectural elements to 
be found in the local environment. Al Madina A´Zarqa is planned to be a model 
for those seeking relaxation in an integrated community that meets the need of 
daily life, including family and work requirements”. <br>
<br>
The highlight of the groundbreaking event was the laying of the foundation stone 
of the first phase by His Highness Sayyid Asad Bin Tariq Al Said. Guests at the 
gala ceremony were treated to a stirring musical performance by renowned singer 
Stefanie Rose Airey, accompanied by Paul Schwartz. A short film titled ‘A New 
City is Born in Oman’, was also aired on the occasion. Shown in two sections, 
the film depicted the past and present of Omani architecture, and included an 
impressive presentation of Al Madina A’Zarqa. Virtual speeches were given by 
Gerard Evenden, senior partner, Foster + Partners, and Dimitris Kallitsantsis, 
director of AECO Development and managing director of Elliniki Technodomiki.
</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="news3.jpg" width="450" height="470"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
Dimitris Kallitsantsis informed, “Enka from Turkey and Elliniki Technodomiki 
from Greece, the shareholders of AECO Development, which is responsible for the 
first phase of this project, are fully committed to this target. The temporary 
city, which will accommodate more than 6,000 people working on this project, as 
well as the infrastructure of this large job-site are nearing completion. The 
first team of experts, consisting of more than 150 persons, completed all the 
pre-construction tasks in close and constructive cooperation with the respective 
teams of ASIT and various government agencies. <br>
<br>
“More than 70 million working hours and almost one million cubic metres of 
concrete will be required for the construction of phase one, which involves more 
than 1,500,000 sq. m. of buildings and the respective urban infrastructure. All 
these figures only refer to the existing first phase; however, they do reflect 
the project’s scale.”
