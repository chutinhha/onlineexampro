<font face="Verdana" size="1"><br>The Capital Market Authority adopted gradual approach in the implementation of 
the principles of corporate governance in the Sultanate. It embarked, since its 
establishment in 1999, on a gradual build up of the principles of corporate 
governance which was culminated by the emergence of the Code of Corporate 
Governance in the first half of 2002 and then full fledge implementation of the 
Code in early 2004.<br>
<br>The code sets out the governance structure of public joint stock companies with 
a view to ensuring the attainment of the interests of the company and the rights 
of all shareholders. It covers the rules and conditions for membership of the 
boards of directors of public companies and the formation of the board and its 
functions. The Code also regulates related party transactions to preclude misuse 
by some directors of their powers for personal benefits.<br><br>A Code of Governance for Insurance Companies was issued recently after the 
transfer of insurance jurisdiction to CMA. To enhance this role CMA established 
the Department of Corporate Governance to take over all the functions and 
responsibilities related to Corporate Governance. On January 1, 2008, the 
Committee of Corporate Governance was established comprising a number of 
government departments to set up a proposal for the implementation of the 
principles and standards of corporate governance for the all the institutions in 
the Sultanate.<br><b><br>Capital Market Authority Annual Corporate Governance 
Conference</b><br>CMA CG Conference is designed to promote knowledge transfer 
and the development of sound and efficient financial markets and banking 
systems, and help shape the changing corporate governance landscape in the 
sultanate. The event will take place on April 21, 2008 at Grand Hyatt Hotel in 
Muscat.<br><b>CMA CG Conference Programme:</b><br>8-8.30 am – Registration<br>
8.30-9 am – Opening remarks by HE Yahya Bin Said Al-Jabri, CMA’s Executive 
President
