		<td bgcolor="#429EBD">
		<p align="justify" style="margin-left: 5px; margin-right: 5px"><b>
		<font face="Verdana" size="1" color="#FFFFFF">The newly-appointed Director for Planning and Operations of Blue City Company 1, 
Michael Lawrence, brings an extensive range of real estate experience and 
expertise to the table. Apart from having been associated with reputed 
international construction firms among them the globally-renowned Atkins, he 
also has the advantage of regional experience in the sector. His role in the 
Blue City project extends from facilities management and utilities to everything 
related to operational activities once the City is complete. In an exclusive 
interview with Oman Economic Review Lawrence talks about the project’s 
development, it impact on Oman’s economy and how it will spur investment. Mayank 
Singh reports</font></b></td>
	</tr>
</table>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<img border="0" src="face2face.jpg" align="right" vspace="5" width="250" height="262" hspace="5"><b>Could you please bring us up to speed with Blue City?</b><br>
The total project is currently divided into 10 phases and as it develops it may 
be adjusted to market demands. In Phase 1 all planning is complete; the detailed 
design is underway and the mobilisation of construction has taken place. We’ve 
now completed the camp for labourers onsite. This is currently planned for 7,000 
people who will be resident on site in labour and construction facilities. <br>
<br>
Phase 1 consists of 5,170 apartments and 380 villas plus associated 
infrastructure like a shopping area, civil defence and a Kindergarten school. 
The master plan design has been done the first five sections of phase-1; we have 
had the concept design done as well and are progressing into detail design. As 
with all big projects the construction and design are running in parallel. <br>
<br>
What I’ve come on board to do is arrange for utilities companies to come in, 
which is a mix of public investment from Omani companies and private investment. 
We are in the process of evaluating the structure of the investors coming in.<br>
<br>
We are also in the process of finalising a partnership with a big American 
facilities management company and they’re now starting to look at support 
operations in the city. We have people joining all the time we are continually 
adding to our team as the project advances. <br>
<br>
<b>This is a huge project and, in fact, one of the biggest in Middle East. It’s a 
complete township and a lot of due diligence went into the project. Could you 
elaborate on these since they were identified as your differentiating factors?
</b>
<br>
<br>
We are going to be a completely integrated city which means we’ve got to 
integrate all public infrastructure with private development. This will included 
general hospitals, a university, cultural facilities and schools. And a 
particular challenge for us is to do all this in an environmentally sensitive 
way. We’ve carried out environmental impact studies on phase 1 and that is a 
continuous process. The other thing is that we want to make a city which is very 
Omani in character and is a modern interpretation of Omani culture. In that 
respect the city is going to be very dense but it’s not going to be very high 
rise. <br>
<br>
Also very important – particularly in projects of this size – is environmental 
sustainability. We also have meetings with the Omani authorities to make sure we 
can reduce the electrical demand on the city. We are trying as far as possible 
to use self-generating methods and we’re investigating measures to generate our 
own electricity through solar or wind. So, all these are measures that any new 
modern city these days has to look at. <br>
<br>
<b>You have brought out a charter of rights and responsibilities. Could you shed 
some light on that?</b><br>
<br>
There’s a legal structure to a project this size, which starts with the 
development agreement we have with the government. From that we have a master 
declaration for the city which sets out rules and regulations for the city. We 
will then eventually get down to setting community rules. This will control the 
way the city develops and it makes sure we develop it in a sustainable way.<br>
<br>
At the moment we are developing everything ourselves. We want to invite third 
party developers but they need to have an understanding of what the rules of the 
city are.<br>
<br>
<b>What about the marketing for phase 1?</b><br>
For Phase 1 we are already in the market. We’ve sold 400 apartments in the first 
launch and we’ve just launched the second batch of 400 apartments. The first 
ones were targeted at the Omani community, but we do expect over 60 per cent of 
our buyers will come from the GCC region. <br>
<br>
<b>Why should GCC citizens invest in the Blue City?</b><br>
I think it’s attractive to the GCC investor if they are looking for investments 
within the region. Oman as a country offers something different to others in the 
region. We benefit from the advantage Oman has in terms of its culture, heritage 
and scenery and we are adding to that by building a community which is not just 
a tourist development. It will have educational and health facilities and 
associated infrastructure. We are not looking at a resort type of environment. 
We’re going be more than a resort; we’re going to be a place where people live. 
Other developments in the region have suffered from being just real estate, 
which is ok if you’re going to stay there for a limited period of time, but if 
you’re going to live there you need facilities such as schools and hospitals 
around you. For people on holiday it’s fine but then they decide to live there 
and they find that they don’t know where to take their children to school. You 
see examples of that.<br>
<br>
<b>Since the Blue City is located far away from Muscat, people may find 
travelling every day to work difficult. So do you think there will be enough job 
opportunities there? </b><br>
<br>
It will be a process obviously but the city will create its own centre of 
gravity. The idea is to attract business here to Blue City and much of that 
business will then feed the real estate side of things. <br>
<br>
<b>It’s an enormous project and this has created a sort of apprehensions in the 
market because there’s been a bit of a slow start. There’s also been some 
negative buzz in the market about the Blue City. How would you react to that? Is 
everything streamlined now?</b><br>
<br>
Yes it is a huge project which requires a huge amount of planning and 
preparation. And it’s important to get it right. In the life of a city 15-20 
years is really nothing. So it’s important to concentrate on these front end 
activities and make sure that we get the mix right. We are benefiting from what 
has been done in the region, taking advantage of what others have been doing as 
well and making sure we get ours right. So, prospective buyers and investors 
should not have any apprehension about the project because at the end of the day 
it has government support, and is backed by a legal agreement. So it is 
happening.<br>
<br>
<b>Another point of some concern is the ownership issue. Can you just clear the 
air on that point?<br>
</b><br>
Unfortunately I cannot really comment on that because the structure of the 
company is such that operations are independent of whatever happens with the 
shareholders.<br>
<br>
<b>When you look around Oman, you see there are at least 40 odd projects coming up. 
So in what way does your project stand out?<br>
We are competing, but I guess the way that we stand out and differentiate 
ourselves is that we are not a resort. We have a resort within the development 
but we have more than that. So we are able to offer people a resort style 
lifestyle but we will, as the project develops, be focussing on companies, 
business and so on.<br>
How much investment will go into the entire project?</b><br>
It’s difficult to estimate the total investment. There are figures of $15 to $20 
billion being talked about. First phase funding is in place and in future phases 
as well we are well underway with negotiating funding as soon as the 
infrastructure is done. Most of the funds are borrowed. <br>
<b>What are the kind of timelines you are looking at for phase 1?</b><br>
Occupation in the first phase will start in Q3 of 2010 and the whole of phase 1 
occupation will be complete by Q4 2012. In parallel to that we will be starting 
work on the other phases as well. It will effectively be like an accelerated 
growth of a city. Construction on infrastructure and earth works will commence 
at the end of the summer and then the first real construction will begin in 
November this year.<br>
<br>
<b>What are the various attractions in phase 1?</b><br>
We are developing an 18-hole golf course, a club and golf hotels. We’ll be 
looking to expand the golf course 27 holes in future phases. In phase 1 we have 
two resort hotels and in addition to that a golf hotel. We are in the 
negotiation phase for operators and they will be international names.<br>
<br>
<b>And concluding thoughts on the project?</b><br>
I think it’s difficult for anybody to get their head around a project of this 
size. To get it organised properly it has taken a lot of effort, which is going 
to continue as we move forward. <br>
