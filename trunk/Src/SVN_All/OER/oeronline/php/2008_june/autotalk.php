        </font><b><font face="Verdana" size="4"><br>
		Feline Grace</font></b><font face="Verdana" size="1"><br>
		<b>Witness the rebirth of a brand that once produced some of the most 
		beautiful cars ever writes Malco&#8209;lm Xavier</b><br>
		<br>
		<br>
		</font><b><font face="Verdana" size="2" color="#CC3300">
		<img border="0" src="autotalk_jaguar.jpg" align="right" hspace="4" vspace="4" width="361" height="289"></font></b><font face="Verdana" size="1">For a long time now, Jaguar has produced easy-to-drive and beautiful 
		cars but it has been trailing behind its German rivals in terms of 
		technology and interior amenities. Also its classic design was not 
		accepted well by most people below the age of 45. But with the release 
		of the phenomenal XK8 in 2006 things changed. Jaguar showcased some very 
		innovative, and truly gorgeous, design themes and technologies inside 
		and out which, they promised, would soon be carried over the rest of the 
		range. The first of the cars in the Jaguar line up to carry this design 
		is the XF – the venerable successor, actually more of a replacement, to 
		the S-type.<br>
		<br>
		The interior of the XF is like nothing Jaguar has ever done before. Its 
		leather stitched dash and the generous amount of wood and aluminium 
		trimming screams out expensive and can now stand alongside some of the 
		best in the industry. The amount of attention Jaguar has paid to the 
		interior is incredible. Starting the car itself gives you a feeling of 
		satisfaction unmatched by any car short of a Porsche 997. As soon as you 
		enter the start button light pulsates with the rhythm of a heartbeat and 
		once you switch it on the way the drive selector raises from its flushed 
		position into the palm of your hand and the once closed AC vents rotate 
		and open up – it is like a world class orchestral performance.<br>
		<br>
		Jaguar has spared no expense when it comes to equipment levels of the XF. 
		Even the most basic of models is equipped with touch screen, touch 
		controls for the lighting and glove box, six-speed semi-auto gearbox 
		with paddle shifters and automatic climate control. Higher up the range 
		you will find voice control, sat-nav, rear-view camera, iPod 
		connectivity and a lot more. Now if only they could equip the car with 
		an iPod…<br>
		<br>
		Space is of the essence in a car of this segment and was something the 
		S-type seriously lacked. But with the XF Jaguar has done a great job in 
		keeping up with the standards. There is more than enough leg and head 
		room in the front and in the back of the XF. However the centre console 
		does intrude into the rear seats making the use of the centre seat a 
		little uncomfortable. There are tonnes of adjustments for the front 
		seats and the steering column so getting a comfortable seating position 
		is never an issue. Also unlike older Jags there is a proper boot with a 
		capacity of nearly 500litres.<br>
		<br>
		Safety has never been an issue for Jaguar, but even this has been 
		stepped up a level. Not only does the XF provide top-notch safety for 
		its occupants, with airbags all round and all manner of driver aids, but 
		it also provides safety for pedestrians with the help of the deployable 
		bonnet system which pops up the bonnet in the event of a collision to 
		absorb the force of the impact.<br>
		<br>
		When Jaguar developed the XF they aimed at creating the quietest car in 
		its class. They mostly succeeded, there is practically no wind, road or 
		any other external noise that we could hear but what you can hear is the 
		beautiful rumble of the engine upfront, which never reaches 
		uncomfortable levels and adds to the sporty feel of the XF.<br>
		<br>
		Jaguar has always produced cars that are great to drive and the XF is no 
		different. It does have a firmer ride then the previous generation 
		S-Type but there is no jarring. At high speeds the XF displays a sense 
		of balance and stability shared by only the best in its class. Through 
		corners the body control is great but there is a slight tendency to 
		understeer, nothing that distracts you from the driving though. You can 
		expect even this to be absent in the models equipped with Jaguar’s 
		active damping system called CATS.<br>
		<br>
		The XF is equipped with a modified version of the quick-changing 
		six-speed transmission present in the XK and is controlled by a flashy 
		knob called the Jaguar Drive Selector. This may seem like a gimmick but 
		it genuinely is easier to use than the conventional stick and when 
		combined with the steering mounted paddles, standard on all models, 
		makes for a very effective package.<br>
		<br>
		The XF, unlike the old S-type, is no longer the least expensive car in 
		its class. But it does bring something refreshingly new to the table and 
		is for anyone who wants a sports coupe with four doors. It also suits 
		anyone who would like to own a premium modern mid sized luxury sedan. 
		With the XF you definitely get your money’s worth.</font></p>
