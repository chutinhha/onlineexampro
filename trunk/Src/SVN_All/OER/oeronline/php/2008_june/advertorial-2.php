</font><b><font face="Verdana" size="4">Qatar Gets its First Environmental 
Awards<br>
</font></b><font face="Verdana" size="1"><br>
<b>Qatar, like much of the world outside it, is witnessing a spurt in 
environmental activism. Green buildings, marine life conservation, raising 
awareness, preservation of endangered species, beach clean-ups, promotion of 
earth friendly practices and higher-educational reforms, hardly scratch the 
surface of the surge in eco-initiatives in Qatar.<br>
</b><br>
<img border="0" src="close_up3.jpg" align="right" vspace="5" width="210" height="206">Undeniably abysmal facts and figures on the peninsula’s deteriorating 
environmental status has prompted many to care, to change, to make a difference. 
Among the most horrifying allegations thrust at Qatar’s environmental record, is 
that the country is one of the top five of the world’s worst ecological 
offenders, that it’s home to over 20 species facing the prospect of extinction, 
and that the state has a high usage (and wastage) of water, at a staggering 300 
million gallons a day, in a region with an alarmingly decreasing supply of 
water. In the wake of this dismal reality it is little wonder that so many 
players – environmental groups, corporate houses and institutions – are engaging 
in a relentless pursuit to save what is left of the natural world.<br>
<br>
While the problem looms large and the numbers seeking a solution keep growing, 
yet a means of recognising and promoting these laudable voluntary contributions 
still remains a void in Qatar’s green scene. The Qatar Today Green Awards, 
spearheaded by Oryx Advertising Company WLL, publishers of the Qatar Today 
magazine, was created fill this lacuna.<br>
<br>
<img border="0" src="qt.jpg" align="left" vspace="5" width="103" height="256">The Awards was primarily created to recognise and promote environmental 
initiatives and achievements across a broad spectrum of sectors ranging from 
corporate houses to institutions to individuals. As Qatar’s first ever 
environmental awards, it also aims to provide environmental groups with a 
platform to make a coordinated effort and to create &amp; increase awareness and to 
mobilise &amp; motivate greater action in this arena.<br>
<br>
The Awards will honour a broad spectrum of environmental activism, from 
grassroots to national, from single endeavours to activities reflecting the 
goals set out in long term environmental visions and polices. Featured in the 
award categories are energy, health and construction sectors – three of Qatar’s 
largest and fastest growing industries – together with other corporate houses. 
Environmental contributions of schools and educational institutions stand to be 
recognised in the “Youth &amp; Community” Award category. Additionally the Awards 
will also acknowledge individual environmental achievements, eco-consciousness 
in transportation and the creation of awareness. A panel of eminent judges will 
choose winners in each category, who will be honoured at the Qatar Today Green 
Awards ceremony scheduled for October 2008.<br>
<br>
Presenting the Awards with Oryx Advertising Company WLL is HSBC in association 
with Qatar Foundation for Education, Science &amp; Community Development and Sharq 
Village &amp; Spa. Oryx Advertising Co. W.L.L is one of the oldest and biggest 
publishing houses in Qatar. Established over 30 years ago, it is the only media 
house in the country that produces three monthly magazines – Qatar Today, Qatar 
Al Yom and Woman Today and a quarterly – The Connoisseur, all leaders in their 
categories.<br>
<br>
HSBC is backed by strong environmental portfolio with both a global and local 
Corporate Responsibility strategy, which includes HSBC’s Climate Partnership 
programme. Under this partnership the Bank has committed $100 million in support 
of various environmental institutions in their efforts to combat global warming.<br>
<br>
Qatar Foundation is no amateur to environmentalism. Its student residence halls 
project, currently under construction at Education City is certified by US Green 
Building Council with the status of platinum LEED (Leadership in Energy and 
Environmental Design). The future dormitories will enable resident students to 
live in zero-waste halls and implement green lifestyles. In addition, Qatar 
Foundation has announced plans for a Qatar Institute for Environmental Research.<br>
<br>
Sharq Village &amp; Spa, owned and operated by the Ritz-Carlton Hotel Company 
implements a wide range of environmental solutions through its Ritz 
Environmental Action Conservation Team (REACT) with a primary focus on energy 
and water conservation and waste management.</font></p>

