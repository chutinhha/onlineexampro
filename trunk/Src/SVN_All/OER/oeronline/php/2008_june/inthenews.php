<b><font face="Verdana" size="4">Major investments underscore Sohar’s global profile</font></b><font face="Verdana" size="1"><br>
<br>
<img border="0" src="soharport_rotterdam_agreement.jpg" width="261" height="180" align="left" vspace="3">The Sohar Industrial Port is headed towards global status following a recent 
spate of agreements outlining major investments and industrial joint ventures in 
the port. Significant among them is a vast Brazilian investment of over 
US$1billion towards the setting up of an iron ore pellet plant.<br>
<br>
The Government of Oman and the Port of Rotterdam have also entered into working 
partnerships with giant Indian Infrastructure specialist, Skil, for the 
development of a 4,300-hectare Special Economic Zone. Further, the establishment 
of an environment agency linked to international standards of monitoring and 
control only underlines the giant growth strides at the industrial port and its 
awareness of its environmental responsibilities.<br>
<br>
In Rotterdam for the signing of a series of agreements were Ahmed bin Abdulnabi 
Macki, Minister of National Economy, Maqbool bin Ali Sultan, Minister of 
Commerce and Industry, and Sayyid Hamoud bin Faisal al Busaidy, Minister of 
Environment and Climate Affairs. The approximately US$1 billion deal which is 
subject to the approval of the board of Brazilian mining giant by Companhia Vale 
do Rio Doce (Vale), envisages the construction of both, an iron ore pellet plant 
and an iron ore distribution centre.<br>
<br>
As per the agreement, signed in Rotterdam, Vale, the biggest producer of iron 
ore in the world, will ship ore from its mines in Brazil to Sohar where it will 
be converted into pellets which will then be exported to the Gulf, Middle East 
and South Eastern Asian markets. Production of the pellets is expected to start 
in the second half of 2010, and it will be the first investment in this sector 
outside Brazil.<br>
<br>
This will be the first wholly owned green field investment in the ferrous sector 
outside Brazil for Vale. The plant, which will be one of the biggest iron 
projects in the Middle East, is expected to contribute to the establishment of a 
number of downstream units and support service projects, besides creating a 
considerable number of job opportunities.<br>
<br>
Also on the anvil is a plan to construct a special deep-water jetty this year to 
accommodate very large vessels with drafts of up to 23 metres, which will be 
hauling in the iron ore from Brazil. Yet another milestone move, and a very 
significant one, was the agreement between the joint owners of the port, the 
Sultanate of Oman and the Port of Rotterdam, with the India-based Skil 
Infrastructure Ltd to develop a free zone in the vicinity of the industrial 
estate. The Indian firm has major infrastructural projects and expertise to its 
credit among them the development of ports, railways and special economic zones, 
and has won international acclaim for its achievements.<br>
<br>
Skil is known internationally for its skills in developing the SEZ concept and 
will be bringing its expertise to help build Sohar’s 4,300 hectare SEZ, expected 
to be launched at the end of this year. The Rotterdam signings also included an 
agreement to create a new environment agency in Sohar linked to the 
internationally recognised DCMR Environmental Agency. The Sohar agency will come 
directly under the Ministry of Environmental and Climate Affairs. <br>
