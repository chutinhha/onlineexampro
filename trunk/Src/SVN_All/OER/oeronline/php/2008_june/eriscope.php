		<b><font face="Verdana" size="4">A Nervous Bull’s Case</font></b><font face="Verdana" size="1"><br>
		<b>Despite the gloomy scenario in Asian banks, there are compelling 
		value plays in Asian finance that simply cannot be ignored any longer</b><br>
		<br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="matein.jpg" width="150" height="237" align="left"></font></b><font face="Verdana" size="1">Asian 
		banks, fund managers and insurance companies experienced collateral 
		damage following the Wall Street credit meltdown and the subsequent 
		repricing of risk across all emerging markets. Moreover, as Asian 
		central banks tightened monetary policy to combat inflation, it was 
		inevitable that speculative bubbles in regional stock exchanges would 
		pop and offshore investors would scramble to limit their exposure and 
		unwind one of the world’s most epic leveraged trades. This was the 
		reason Shanghai’s A share market, Vietnam, India’s Sensex, Hong Kong’s 
		Hang Seng and China Enterprise Index lost 30-50 per cent of their market 
		capitalisation in the past six months. Admittedly, some Asian banks, 
		such as Singapore’s DBS, India’s ICICI Bank, China’s ICBC and Bank of 
		China, admitted to significant CDO exposures. This is a necessary, but 
		not sufficient condition for a rally in Asian banking, as the capital 
		markets must be convinced that there are no more significant CDO/ 
		subprime exposures in the banking closet.<br>
		<br>
		A second condition for any significant rally is hint of a resolution to 
		the credit crisis and the restoration of confidence in the international 
		interbank market. While this scenario is still wishful thinking, the 
		Federal Reserve has responded, in its role as the lender of the last 
		resort, to systematic banking distress and opened its discount window to 
		securities broker/ dealers for the first time since the Great 
		Depression. Accordingly, it engineered the bailout of Bear Stearns via J 
		P Morgan’s takeover bid apart from accepting US$200-billion mortgage 
		backed securities collateral on its balance sheet. But even then, US 
		home prices can still decline at a faster rate, the first consumer 
		recession since 1991 can deepen and wreak havoc on Asian exports, a 
		major global bank, hedge fund or securities firm can go belly up. The 
		bear market in commercial real estate can widen from the US and Europe 
		to Asia.<br>
		<b><br>
		Between the landmines<img border="0" src="periscope1.jpg" align="right" width="250" height="184"></b><br>
		However, I believe the Armageddon scenario is mostly priced into the 
		valuation of Asian banking and the Bernanke’s historic moves as the 
		Federal Reserve chief on the Bear Stearns front and the discount window 
		have removed the excessive fear premium that has been haunting the 
		financial markets. The 200 basis points fall in dollar interest rates 
		since September and the credit contraction that forced Western banks to 
		reduce their balance sheet exposure to emerging markets can even mean 
		that the credit crunch will actually benefit the competitive positioning 
		of Asian banks. Landmines exist in bank shares all over the world, 
		particularly in the Gulf, Malaysia, India and China. Yet, there are also 
		compelling value plays in Asian finance that I simply cannot ignore any 
		longer.<br>
		<br>
		Take Chinese banks listed in Hong Kong as an example. The HKCEI was 
		devastated by the bear market in Shanghai, as the Peoples Bank of China 
		(PBOC) tightened interest rates to combat the highest CPI in a decade, 
		Asian hedge funds slashed long exposures, retail speculation in warrants 
		and IPO’s triggered a crash, as valuations rose to stratospheric levels 
		last November. But I believe that the risk/return calculus for Hong Kong 
		listed Chinese shares is a lot more realistic after the 40 per cent 
		plunge in the index. One, index valuations have plunged from 28 to 13 
		times forward earnings and price/ book value metrics have compressed 
		from a bubble 4X to a reasonable 2.4X now. Moreover, Henry Paulson’s 
		visit to Beijing and the PBOC’s inflation hawkishness means an 
		accelerated pace of Renminbi revaluation, and this is a positive 
		translation gain for Chinese banks, telecom shares and oil companies who 
		dominate the index.<br>
		<br>
		Moreover, Asian hedge funds desperate for performance will have to raise 
		exposure from 40 per cent as long as the indices rally and QDII funds 
		finally begin to trickle into the shell-shocked stock market of the 
		fabled Crown Colony/ SAR on the South China Sea. It is, of course, 
		imperative for investors in Asian banking to use disciplined risk 
		metrics, entry points, even take advantage of juicy risk premia in the 
		listed/ OTC derivative markets. <br>
		<b><br>
		Some key plays</b><br>
		Take, for instance, Bank of Communications. It trades at a reasonable 
		16X earnings and 3 times book value, after its 35 per cent fall since 
		New Year, and has no subprime or even dollar exposure. More importantly, 
		it has HSBC as a strategic shareholder. At HK8.50, I will buy Bcom for 
		target over the next 12 months, as Bcom also benefits from rising 
		Renminbi, debt yields, rising net interest margins and rising fee 
		income.<br>
		<br>
		I am also convinced that the victory of the KMT in the Presidential and 
		Legislative Yuan is going to lead to a secular re-rating of the 
		Taiwanese stock market. President Chen’s belligerence was a disaster for 
		Taiwan’s relations with the PRC, whose Politburo raised “the renegade 
		province” rhetoric a few decibel points. President-elect Ma Jing 
		promises to end the cold war with Beijing and this will be a nirvana for 
		Taiwan bank shares, which benefit most from more tourists, more Mainland 
		money in land and buildings and more government spending, apart from 
		more capital flows. While the rising Taiwan dollar and uncertainties in 
		Silicon Valley (tech is 50 per cent of Taiwan’s market cap and earnings) 
		may limit the upside above 10,000 on the Taipei index, local banks can 
		do far better. My play here is Chinatrust Financials at 28 Taiwan 
		dollars for a 35 target.<br>
		<br>
		In India, the New York ADR of ICICI Bank (symbol IBN) has plunged from 
		74 to 40 now. The $260 million mark to market losses in the CDO book, 
		the inflation hawkishness of the RBI, lower loan book growth, rising NPL 
		in mortgages/vehicles loans and the equity shock (5,000-point fall in 
		Sensex) in the valuation of ICICI Prudential and ICICI Securities has 
		had a devastating impact on banking valuations. Moreover, the 
		international banking subsidiaries of ICICI bank in Canada, Britain, 
		Bahrain, Singapore and the US will face higher funding costs in the 
		interbank market. So, an ideal strategy will be to sell the September 
		2008 puts on the bank’s ADR. My optimal range for IBN is to buy at 32, 
		sell at 45. <br>
