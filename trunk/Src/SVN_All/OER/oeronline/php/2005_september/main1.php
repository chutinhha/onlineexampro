<img border="0" src="face1.jpg" width="120" height="115" align="left" hspace="5" vspace="2">With operations in more than 50 countries, 
Royal &amp; SunAlliance is a leading international insurer, which has built upon a 
worldwide reputation and experience to create a strong, growing position in the 
Oman market. Mehre Alam chats with Nigel Forster, Managing Director, Royal &amp; 
SunAlliance, Oman, to chart out the latest on his company and its presence in 
Oman</b><br>
<br>
<b>As part of one of the oldest insurance companies of the world, how do you 
evaluate the growth of insurance companies around the world in general and the 
Middle East in particular?</b><br>
There are good growth prospects across a variety of both established and 
emerging markets around the globe going forward. Within Royal &amp; SunAlliance this 
year, we are seeing solid double-digit growth from our established UK Personal, 
Scandinavian, Canadian and Latin American operations. <br>
<br>
China and India, arguably the two most challenging and promising emerging 
insurance markets, are also beginning to achieve real growth. In tandem with 
robust economic development, their insurance markets have grown spectacularly. 
Life insurance premiums in China have grown by an annual average of 23.7 per 
cent over the last decade, while non-life premiums have grown by 10.8 per cent 
over the same period. Average annual growth rates for insurance in India were 
12.7 per cent for life and 6.2 per cent for non-life. In 2003, China and India 
were respectively the 8th and 18th largest life insurance markets in the world. 
Their rankings are 13th and 28th in terms of non-life insurance.<br>
<br>
Despite these impressive growth rates, these two markets are still relatively 
small, accounting collectively for only 2.2 per cent of global insurance 
premiums. Nevertheless, their huge economies and population sizes, coupled with 
rapid industrialization and globalization, should create ample opportunities for 
the development of insurance. In particularly, liberalization and deregulation 
are rendering these newly de-monopolized markets more accessible and attractive 
to foreign insurers. Currently, majority foreign-owned insurers account for less 
than 3 per cent of insurance premiums in China. India restricts foreign equity 
participation to 26 per cent in joint ventures. At the same time, insurance 
regulations are maturing to levels commensurate with international best 
practice.<br>
<br>
Prospects within the Middle East are also sound primarily due to sustained 
economic growth and diversification away from historical revenue streams, with 
both insurance and reinsurance markets enjoying a reasonable level of growth in 
recent years. In addition, there has been a move to more compulsory lines of 
insurance in Saudi Arabia and this looks certain to be followed by other Arab 
countries.<br>
<br>
<b>Where does the insurance sector in Oman stand with regard to the other 
countries of the GCC?</b><br>
In terms of relative combined life and non-life Gross Written Premium [GWP] 
market statistics, Oman represents the fourth largest GCC market behind Saudi 
Arabia, UAE and Kuwait. <br>
Oman’s GWP and GDP Per Capita figures of approximately $US 110 and $8,250 are 
some of the lowest within the GCC. However, with GWP as a percentage of GDP 
totaling 1.33 per cent, this represents one of the highest ratios within the 
GCC, albeit very low in comparison with many Western markets. <br>
<br>
<b>Is there a lack of awareness among people in the Sultanate about the 
importance of some insurance products?</b><br>
My own view is that there is a broad lack of awareness in the Sultanate about 
insurance and this is evidenced by the relatively low GWP Per Capita figures as 
earlier discussed. <br>
Like elsewhere in the Islamic world, it is fair to say that there is at first a 
reluctance to the concept of insurance as it can be seen to be hedging against 
fate. Insurance does, however, play a very significant role in terms of enabling 
balanced and sustainable economic growth through alleviating financial losses 
through risk transfer.<br>
<br>
It is the collective responsibility of all of us working in the industry to 
raise both public awareness and understanding of the role insurance plays in 
society. <br>
<br>
<b>What are the growth prospects within the Oman insurance sector?</b><br>
I see the major commercial premium growth prospects in Oman as essentially being 
driven by opportunities within three broad areas, namely gas, industry and 
tourism. In terms of personal lines portfolio growth, while there is evidence 
that this segment of the market is starting to grow, it is still a relatively 
underdeveloped market.<br>
<br>
Growth is also being achieved locally by some insurers, as they explore new 
distribution channels such as bank assurance and linking with non-banking 
financial companies (NBFC’s). Whilst we may also review our position in terms of 
product distribution going forward, our main focus will always be in working 
with our broker partners within the local market to develop profitable new 
business. <br>
<br>
<b>What’s the unique selling point of your company?</b><br>
Quite simply…our people and when I say that I mean our local team, including our 
JV partners, in addition to the other 30,000 employees of Royal &amp; SunAlliance 
Plc UK worldwide.<br>
<br>
By way of background, Royal &amp; SunAlliance has been operating in Oman since 1972, 
originally as an agency and later as a branch of Royal &amp; SunAlliance Plc UK. <br>
<br>
In 2004 we launched Royal &amp; SunAlliance Insurance Oman SAOC, a Joint Venture 
company in which Royal &amp; SunAlliance holds 67 per cent and our partners W.J. 
Towell &amp; Company and Oman Holdings International holding the balance. Our 
partners are highly respected and have wide ranging business interests, which 
include property development, consumer goods, telecommunications, motor 
vehicles, oilfield equipment and retail.<br>
<br>
We have an experienced and professional team locally and are currently running 
at an Omanisation level of 65 per cent. This aligned with the worldwide 
reputation, experience and resources available within the Royal &amp; SunAlliance 
Group makes for a strong local proposition with global reach – our USP.</font></p>
