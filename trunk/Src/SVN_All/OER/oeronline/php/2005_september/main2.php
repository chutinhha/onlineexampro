<font face="Verdana" size="1"><b>
<img border="0" src="face2.jpg" width="100" height="95" align="left" hspace="5" vspace="2">OER gets face2face with Munir M. Khan, GM of 
Sadolin Paints, on the paints leader’s new strategic initiatives and the road 
map ahead<br>
</b>
<br>
<b>Tell us something about your career profile</b><br>
I’m basically from India. I did my engineering from REC (Surathkal) and PGDM 
from IIM-Kolkata. Thereafter, I joined Asian Paints and within nine years, I was 
made the youngest General Manager in the company’s history. After spending 13 
years in various functions, including retail and industrial sales, factory 
management, personnel management, systems management and project management 
etc., I moved to Sadolin Paints (Oman) in 1984. I liked the company and the 
country so much that I have spent 21 years here.<br>
<br>
<b>What’s the secret of Sadolin’s leadership in paints business in Oman?</b><br>
Sadolin Paints (Oman) Ltd., was the first paint company to start production in 
1977 and continues to be the number one paints company in Oman. We enjoy a 
market share of 40 per cent within Oman and 35-40 per cent of our business comes 
through exports. Our company’s success is based on our strategy of focusing 
primarily on – high quality through best technology; customer focus orientation 
at all levels and following the principle of continuous improvement (Japanese 
strategy called Kaizen, which means continuous improvement). This is the reason 
why we are the preferred partners in Oman for any international paint’s giant 
that eyes at GCC market for business expansion. <br>
<br>
We are constantly endeavouring to partner with the best companies in the world 
to deliver world-class products to the retail and industrial customers in Oman 
and regional markets. <br>
<br>
<b>Which are your major strategic international partners? </b><br>
We are perhaps the only company of its kind in the region with access to the 
best multiple international technologies. For decorative coatings and wood 
protection, which comprises a major chunk of our business, we have a licensing 
arrangement with Sadolin Paints, Denmark, a 225-year-old company, which is now 
part of Akzo Nobel. For furniture lacquers also, we have licensing arrangements 
with M/s.Akzo Nobel, Italy – world leaders in coating industry. We have a 
technological partnership with Stoncor, USA, for protective coatings. We are the 
distributors of PPG Automotive Refinishes, the world leaders in refinishes for 
Oman. <br>
<br>
In August this year, we have formed an alliance for manufacturing and 
distribution with Tyco Adhesives Corrosion protection Group to produce 
Powercrete liquid epoxy coatings, which are used for corrosion prevention of 
oil, gas, water and wastewater pipelines. <br>
<br>
We are always trying to improvise so that our company remains a world-class 
competitive player, which is well respected and envied by competitors. Perhaps 
that is the reason why most of these international giants have preferred to join 
hands with us ahead of many other companies in the GCC region. <br>
<br>
<b>What kind of manufacturing and distribution infrastructure do you have?</b><br>
We have a modern manufacturing plant and laboratory at Rusayl Industrial Estate. 
We have the capacity to produce 15-million litres of paints and adhesives. We 
have built the largest network of dealers and established a wide network of 
Colour Points in the Sultanate. We have branches with depot facilities at Dubai, 
Bahrain and Qatar. We have distributors in Kuwait, Saudi Arabia and Yemen also.
<br>
<br>
<b>Which are the major countries where you export your products to?</b><br>
We export mainly to the GCC countries. Plus, we export comparatively smaller 
quantities to countries like Syria, India and Sudan as well. We are exploring 
markets outside the GCC region to further expand our business. <br>
Setting up of a subsidiary in Kochi, India, is part of this expansion game plan. 
We started with wood products in India but recently we have launched our 
decorative paints in that market also. The initiative has been quite successful 
and we are definitely going to build on this base. <br>
<br>
<b>With infrastructure development in full swing in the GCC region, the paints 
business must be growing at a faster rate?</b><br>
Though different product categories are growing at different rates but overall, 
the paints business is on the upswing. Our company is growing at more than 10 
per cent rate average in the last 20 years. The unexpected rise in oil prices in 
the international market will give further momentum to our business. The launch 
of massive development projects in most of the GCC countries has definitely 
given us an opportunity to roll out a wide range of products to meet the varied 
demands of the industry. <br>
<br>
To benefit from the economic boom in the GCC region, a host of international 
paints companies are vying with each other to enter this market by forming 
alliances with top GCC based companies. This kind of an opportunity didn’t exist 
a couple of years back. <br>
<br>
<b>So what is the strategy of your company to maximize the returns in the boom 
time? </b><br>
The GCC market is mainly guided by the sentiments and fluctuations in the oil 
market prices. But our focus is to grow irrespective of the developments in the 
oil market. If one sector is not doing well, we try to identify opportunities in 
other sectors, which are growing at a faster rate. Originally, when we had 
started, we were manufacturing and distributing decorative paints only, that too 
a limited variety. Today we have a complete range of decorative coatings, 
presence in wood protection products, furniture lacquers, tank linings, 
protective coatings and adhesives etc. We keep analyzing the developments in 
different industry sectors to anticipate the fluctuation in the demand for our 
products. We have an excellent range of products with application in a wide 
range of industries. <br>
<br>
<b>How has the paints industry shaped up in Oman in the last few years? </b><br>
It has come a long way. At one point of time, we were the only local 
manufacturing company in this business. In 1985, two more companies joined in. 
Another three players moved in sometime in 1997-98. So today there are six local 
players in this market. The market is becoming more competitive and tough not 
just in Oman but MENA (Middle East North Africa) as well. All the countries in 
MENA have got multiple paint-manufacturing units with excess production 
capacities. Exports business in the paint industry has always been very 
difficult. Thanks to our wide range of quality niche products, we are able to 
export 35-40 per cent of our production. <br>
<br>
<b>Where do you see your company and the market moving in the next three years?</b><br>
We would like to continue to maintain our leadership and in fact increase the 
gap between us and the other players in the market. In the long term, it is 
difficult to predict the growth trend, as this market is very sensitive to oil 
prices. There is no steady growth on year on year basis as the paints market 
fluctuates with oil price changes and the resultant construction activity. One 
year it could go up by 15 per cent and next year it could go down by 10 per cent 
also. However, for next three years, oil prices are expected to be high, so the 
immediate future is looking very bright.<br>
<br>
<b>What steps are being taken by your company to meet environment regulations?</b><br>
We firmly believe in maintaining our environment and have ensured strict 
processes and controls to meet the environment protection laws. Our partnerships 
with leading international companies provide us access to the most modern 
technologies, which meet the stringent laws existing in Europe and America. We 
find that Denmark is most stringent on environment and we at Sadolin are largely 
influenced by the parent company in this regard.<br>
We have been trendsetters on the social responsibility front in Oman. We were 
the first paint company in the Middle East to get both ISO9000 and 14001 
certifications. Many people advised us not to incur expenses by going for ISO 
certifications but we went ahead to ensure best business practices as per the 
international norms. Look at the benefits today. <br>
<br>
In fact, in July, we have already been audited by DNV for OHSAS 18001 (1999) 
standard and recommended for certification. Once again, we are the first paint 
company in the Middle East to be so certified. I might add that M/s.Tyco 
Adhesives have joined hands with us due to our progressive outlook and excellent 
professional organizational team. <br>
<br>
Tyco’s Powercrete® PW is an ANSI/ NSF certified product – meant for potable 
water pipelines. NSF approval is required not only for the formulation but also 
for the site where it is manufactured. We have therefore, been already audited 
by NSF international and recommended for certification in August.</font></p>
