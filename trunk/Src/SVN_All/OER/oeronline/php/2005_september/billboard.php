                    <b>MHD launches XDS System</b><br>
                    MHD LLC’s automotive parts &amp; service division has introduced 
                    the XDS Glass Strengthening System in Oman. The innovative 
                    system promises protection for the glass from accidental 
                    shattering, storms, earthquakes, burglary, vandalism, 
                    firebombs, small firearms and explosions etc. Developed by a 
                    US based manufacturer and distributed by Dubai based 3S 
                    Solutions, XDS comes with a lifetime warranty against 
                    peeling and discoloration etc. The system mainly consists of 
                    two parts, a super tough Mylar polyester with a 
                    scratch-resistant coating, and a specially formulated and 
                    patented high temperature acrylic adhesive, which when 
                    applied to the glass, infiltrates the glass pores, gets 
                    absorbed and hardens, to strengthen the glass itself. The 
                    glass actually repels objects hurled at it. The company 
                    claims that CNN had reported that this product had saved 
                    many lives during the 9/11 Pentagon incident.<br>
                    <br>
                    Moreover, XDS also acts as a thermal protective film 
                    absorbing more than 98 per cent of the skin disease causing 
                    ultra violet (UV) rays. It results in benefits like less 
                    heat preventing overall discomfort and fading of 
                    household/office items.</span></font></p>
