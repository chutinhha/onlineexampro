Sunday, 24 July 2005, His Majesty Sultan Qaboos bin Said issued Sultani Decree 
75/2005 (Amending Law) amending certain provisions of the Oman Penal Code and 
the Law of Criminal Procedure. The provisions of the Amending Law become 
effective upon publication in the Official Gazette sometime in early August 
2005, and are expected to have a significant impact on the enforceability of 
judgments against the government in commercial, civil and administrative 
disputes.<br>
<br>
<b>The Penal Code and its Amendments</b><br>
Part II of Sultani Decree 7/74 promulgating the Penal Code of Oman (Penal Code) 
deals primarily with crimes committed against the Public Administration, and 
crimes committed by public employees. Article 163 of the Penal Code provides 
that any public employee who neglects or refuses to implement a legal order 
issued by a judicial or administrative authority shall be sentenced to 
imprisonment for a period ranging from one month to one year.<br>
<br>
The Amending Law adds an additional paragraph to Article 163, further providing 
that the same punishment and dismissal will be applicable to every employee who, 
after receiving a prior notification from a court official, willfully refrains 
from or delays for a period exceeding two weeks from the date of such 
notification the implementation of a judgment or order issued by any court. <br>
<br>
<b>The Law of Criminal Procedure and its Amendments</b><br>
The Law of Criminal Procedure promulgated by Sultani Decree 97/99 (Law of 
Criminal Procedure) provides in Article 5 that a criminal case may not be 
initiated except in two instances. First, upon a verbal or written complaint 
from the victim or his private attorney relating to crimes identified by the 
law, in which case the complaint shall not be accepted after three months from 
the date the victim knows about the crime and the criminal, unless the law 
provides otherwise. Second, upon a written request, or after obtaining written 
permission from the competent authority, relating to crimes that require such a 
request or permission according to the law.<br>
<br>
The Amending Law has added a new article that precedes Article 5. The new 
article provides that notwithstanding Article 5, whoever suffers harm due to the 
crime provided for in the new second paragraph of Article 163 of the Oman Penal 
Code may submit a direct request for summons of the accused to appear before the 
competent misdemeanor court at the time and according to the procedures provided 
for in Articles 163 and 164 of the Penal Code. The article further provides that 
the Registrar of the Court must provide a copy of the statement of the summons 
to the public prosecutor in order that the criminal case may be prosecuted. 
Article 164 of the Penal Code addresses crimes in which public employees divulge 
without legal justification confidential information they have obtained by 
virtue of their office. <br>
<br>
<b>The Effect of the Amendments </b><br>
These amendments to the Penal Code and the Law of Criminal Procedure have 
established new principles regarding the enforcement of judgments against the 
government in commercial, civil and administrative disputes. <br>
<br>
Prior to these amendments, the enforcement of judgments against the government 
was particularly difficult because, by law, public money is not subject to 
attachment or judicial enforcement. These amendments will make the enforcement 
of judgments against the government less prohibitive because a judgment for 
payment of any amount, or for reinstatement of an employee, or for the amendment 
or cancellation of an administrative decision, against any government department 
or unit will immediately be enforced by the competent official in the applicable 
Ministry within two weeks from the date he receives a notice requiring such 
enforcement. A failure to comply with these new amendments would jeopardize such 
official's liberty and future employment. <br>
<br>
The amendments have also introduced a new regime in Oman whereby a plaintiff may 
directly initiate criminal proceedings in respect of offences under Article 163 
without first having to resort to the public prosecutor. <br>
<br>
In such instances, the public prosecutor will receive from the Court a separate 
notice regarding the proceedings. This is in contrast to all other crimes in 
which the court will not take cognizance of a criminal case that has not been 
officially transferred to it by the public prosecutor. <br>
<br>
Furthermore, in view of Sultani Decree No. 72/2001, which categorises as public 
employees those employees of companies in which the government is a shareholder, 
such companies will be required to immediately implement any judgment or 
judicial order, or risk exposing the relevant employee in the company to 
imprisonment of up to one year together with dismissal from his post. <br>
