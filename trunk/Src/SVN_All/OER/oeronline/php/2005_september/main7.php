<p align="justify" dir="ltr"><font face="Verdana" size="1">
<b>The new ML 350 is a perfect choice for the very rich urban lad, and ideal for 
those looking to explore more extreme terrains</b></font><font face="Verdana" size="1"><br>
<br>
<b><img border="1" src="mercedes1.jpg" align="left" width="150" height="85"></b></font><b><font face="Verdana" size="4">T</font></b><font face="Verdana" size="1">here 
is something about SUVs which makes men, even regular 8X4 cubicle dwellers, want 
to don their sporty Tees, pull on those army-style camouflage trousers, round up 
a bunch of old, but excellent motoring buddies, and go off-road! More than 
anything else, it is after all an image, which makes SUVs sell around the world 
and especially in the Middle East. We might only use it to ferry kids to and 
from school, or take wife/fiancée/ girlfriend shopping, may be show off on the 
city roads, but somewhere in the back of your head you can still hold on to all 
those romantic associations of dune bashing during your hey days. Sure, you 
probably wouldn't be setting off to the sandy desert, but with a SUV, it's not 
that you would, but that you could. Someday you just may... <br>
<br>
Now let's cut to the real world, and see what we have here and that is nothing 
less than the legendary Mercedes-Benz ML 350. It has been almost eight years 
since Mercedes-Benz introduced the Alabama-built ML 320 mid-sized SUV, to the 
world of automobiles. Eight years is a long time - and despite numerous engine 
upgrades and styling updates along the way, the ML was starting to show its age, 
particularly in the face of new competition.<br>
<br>
Though the first ML was a successful compromise between a comfortable 
urban/suburban luxury vehicle and a competent off-roader, its slab-sided and 
upright styling was said to resemble a running shoe. The car doesn't really need 
an introduction - it's been the choice for any top-level executive or 
businessmen for many years now.<br>
<br>
However, it has evolved over the years, and it’s been continuously refined by 
engineers -- the M-Class in all its different avatars. They are not a common 
sight on our roads, and looking at the 2006 version, there was an undeniable 
sense of deja vu. Right from those 'Intercooler Turbo' stickers on the flanks, 
to the chunky cladding on the sides, and the 19-inch alloy wheels, this car 
looks familiar.<br>
<br>
So, how is the M-Class to drive? Let's see now. For starters, all the right 
tackle is in place - there's that 3498 CC power-plant that puts out 350 Nm of 
peak torque at 2400-5000rpm, and a simple push of the off road switch that is 
fitted as standard in the M-Class rimes all of its electronic systems for off 
road driving and extra traction. <br>
<br>
Get inside; fire up the engine, and with all the sound insulation, engine 
clatter is minimal and accounted for. Slot it into drive (action is clean and 
progressive), and the car motors off without any fuss whatsoever. The very first 
impressions are of a rather firm yet precise gearshift, and a good turning 
circle. The kerb weight of ML 350 is 2135 kg (mass in ready to drive state with 
fuel tank 90% full, driver weighing 68kg and 7kg luggage) its a big, heavy 
vehicle and you need to be careful while inching through heavy traffic. In 
addition, this is your typical slow-revving tuned-for-torque and overtaking 
moves need to be planned in advance. If you let the revs drop low and then try 
to accelerate without shifting down to a lower gear (if it’s not automatic}, 
there would be considerable driveline drone, and the thing begins to bog down a 
bit. However, once it gets moving, once the turbo starts spooling hard, and once 
the rev-counter needle swings past 2000, the ML 350 doesn't really feel short of 
grunt.<br>
<br>
The steering feels nicely connected, and the car can be hustled along at a fair 
pace. As expected, there is a considerable amount of body roll even while 
cornering at barely moderate speeds, but it doesn't threaten to get out of hand. 
Relaxed cruising is what the Mercedes is best at, and even though the Speedo is 
marked all the way where traffic signboards won’t even think of in the near 
future, we think this machine would be happiest cantering along at any speeds 
that authorities can implement.<br>
<br>
Four-wheel-drive can be selected by pushing a switch, and can be done even while 
on the move. Of course, since low-ratio 4WD exists only to extricate you from 
particularly tricky situations, this mode is handy for going up very steep 
inclines on tricky terrain, keep your foot off the throttle and the car will 
crawl up safely - you just need to steer the thing in the right direction! <br>
<br>
For more extreme terrains, have the optional Off Road pro engineering package 
fitted. It comprises of two engage-able differential locks, a two-speed transfer 
case and a reinforced rear axle differential. More than off-road-ability, 
what'll perhaps be important for most buyers is how comfortable the ML 350 is. 
The long-travel suspension – fording depth, standard suspension – makes sure 
that the occupants remain isolated from the bumps and thumps of a badly broken 
terrain.<br>
<br>
With rows of seats, the ML 350 is meant for five people, though you can probably 
stuff in many more if so inclined! In my opinion, what new buyers will 
appreciate the most about the new ML 350 are the extra interior room and the 
stylish, functional interior design. All passenger seats are fitted with seat 
belts, and there are separate controls for adjusting the air-conditioning for 
the pillion occupants. Seats are comfy, can be adjusted for height and position, 
and front seats even feature adjustable lateral support. Plus, you have a 
tilt-able steering wheel column so finding your ideal driving position should be 
a cinch in this car. The passenger compartment of the 2006 ML 350 is 
considerably roomier than the previous ML, with more front and rear legroom and 
elbowroom. <br>
<br>
The quality of interiors is acceptable, if you are spending RO25,495. Comfort 
and elegant materials hallmark the interior of the new M class. The sense of 
indulgence begins with the standard multifunctional steering wheel, which allows 
you to operate not only the radio and the telephone but also the gearshift 
without having even to take your hands off the wheel. Quality in its finest form 
also materializes in features such as leather-upholstered seats, wood trim with 
chrome inlay, tubular instrument cluster gauges, circular air vents and matt 
finish aluminium. <br>
<br>
The bottom-line is, this ML 350 remains as supremely competent an off-roader as 
it ever was. However, most buyers are simply not likely to indulge in the kind 
of intense off-road driving which ML 350 is optimised for. Let's face it - the 
average urban cowboy who buys these things wouldn't even know what to do with 
lockable differentials and such! If you mostly drive on roads, and expect luxury 
car-like behaviour from your super-expensive SUV, then Mercedes has the current 
generation ML 350. This one uses 7G-TRONIC automatic transmission’ the virtually 
seamless automatic transmission, world’s first ever 7 speed planetary 
transmission. It selects the ideal gear to suit the current driving situation, 
with seven forward gears there are virtually no jumps between them and lastly 
shift times are short meaning that gear changes are practically imperceptible.
<br>
<br>
Which means the new ML 350 is a perfect choice for the very rich urban cowboy, 
and is still the time-trusted antidote (a rather pricey one, we might add) for 
those forever haunted by the visions of a sweltering red horizon, and vertical 
limits. </font></p>

