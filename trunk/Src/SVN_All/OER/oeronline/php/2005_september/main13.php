action plan for success – ten ironclad strategies for winning the battle within 
yourself and mastering the mental game.<br>
<br>
<b>Thou Shalt Have a Great Attitude</b> <br>
Everything in golf, and I mean everything, 
starts with your creating, nurturing, and maintaining a positive attitude for 
success. I like what popular psychologist Dr.Phil McGraw says when he is talking 
about understanding a subject or a lesson point. Dr. Phil tells his audience, 
“Either you get it or you don’t.” This remark applies directly to the importance 
of building a great golfing attitude. Either you “get” the idea that attitude is 
a vital element of your game or you don’t. It’s just that simple.<br>
<br>
The problem with “you get it or you don’t” for many players may stem from the 
notion that a great attitude is an intangible thing that can’t really be 
measured. It’s true that attitude is a psychological-emotional component that 
can’t be measured on a stat sheet<br>
or played back on a video camera. <br>
<br>
However, when a player has a great attitude, you can sense it in the way she 
walks, talks, and acts on the golf course. A golfer with a great attitude stands 
out without having to say a word about her performance. Every player, coach, and 
spectator can spot a player who has a great attitude just by the way that golfer 
handles herself. A golfer with a poor or negative attitude also stands out, 
sometimes even more dramatically; by the way she talks and behaves. The 
following story of a young touring professional provides an excellent 
illustration of the power of one’s attitude.<br>
<br>
<b>Attitude Adjustment Case Study</b><br>
Recently a young professional golfer sought my help to figure out his lack of 
success on Tours. Let’s call him Daniel.<br>
<br>
Daniel felt that he had all of the required tools to be a top player, but he 
couldn’t quite understand why he wasn’t successful. After all, he was one of the 
longest hitters on the PGA Tour and perhaps one of the finest ball 
strikers.Daniel remarked to me, “I just<br>
<br>
don’t get what makes some of the other guys shoot lower scores than me. I’ve 
thought about this for a long time now. I’ve compared my skills to theirs, and 
mechanically, I hit the ball with more authority, I’m longer than almost 
everyone out here, and I sure look a lot more professional than most of them.”&nbsp; 
He went on to say, “I just can’t understand why they are beating me.” <br>
<br>
After listening to him explaining his dilemma in further detail, my response to 
Daniel was simple. I told him that he was absolutely correct in his assessment 
of the situation. Appearing stunned, he asked me what that meant. I explained 
that he was correct in that he “just didn’t get it” and that he would never “get 
it” if he didn’t learn to take a look at his own attitude. This young player was 
so busy telling other players how good he was (when he wasn’t scoring all that 
well) that he was burning bridges with his ego-protecting behaviour. No one 
wants to play with someone who thinks he is better than everyone else. What does 
that say about your game as a playing companion or partner? <br>
<br>
I went on to explain to Daniel that the golfers who were succeeding didn’t care 
about how far he could hit the ball or about the efficiency of his golf swing. 
Each one cared only about his own game and learning to create a personal playing 
attitude for his own success; he cared about how his own thoughts and attitudes 
affected his play, and not Daniel’s. I told him that this was not a selfish 
attitude but a self-full one, meaning that each player is full of himself in a 
positive and nurturing way. Each golfer is ultimately responsible for giving 
himself a positive self-direction for peak performance. Each player needs to 
develop an attitude and mindset that he believes in his talent unconditionally. 
An unconditional positive attitude is where players need to start in seeking 
success because it is in the foundation from which all dreams are created and 
realised. <br>
<br>
<b>Attitude is an Emotional Fuel</b> If you start your golfing day with a poor 
attitude and cannot find anything positive to draw from your round and your 
talent, you will never be the player that you aspire to be. Attitude is like 
emotional fuel. The type of fuel you use determines how far and how fast you 
will go. If you put cheap fuel in your car’s gas tank, your car will eventually 
start to spurt and shut down quickly. Although cheap gas may be all right in the 
beginning, it is destructive to your car’s well being in the long run. On the 
golf course, if you fail to believe in yourself, you are likely to give up and 
become discouraged. Your ability to hang in there and put up a decent score is 
ruined from the start. Your “cheap gas” attitude will become a liability to your 
golf performance. <br>
<br>
However, if you use premium gas in your car, you have given yourself the best 
fuel available to run long and smooth. In the same way, with a great “premium” 
attitude you can endure the shots that go astray and the putts that don’t go in 
because you have the ability to stay focused and upbeat. If you focus on being 
positive and patient you will ultimately see the results on your scorecard. It’s 
all because of your attitude. To put it another way, if you place bad thoughts 
and feelings in your mind, you’re likely to suffer a mental collapse and call it 
quits. Have good thoughts and feelings in your mind and you are off to a good 
day. It’s just that simple.<br>
<br>
Specifically, you need to start your round with a positive attitude and expect 
that good things will happen. And you need to maintain an attitude that provides 
positive momentum for the duration of the round. Finally, you need to have a 
resilient attitude that allows you to accept your bad breaks and generate an 
appropriate and enthusiastic physical response for your next shot.<br>
<br>
Overall, attitude is perhaps the most important club in your bag because it 
ultimately determines your talent and aspiration level. Your attitude is 
directly linked to your motivational motivational interest in yourself. A 
majority of players I work with realise the<br>
importance of attitude and try to start their rounds with a positive mindset, 
but some are not sure how to create an attitude that will endure throughout an 
entire round. That is where I come in and offer some specific strategies for 
their review.<br>
<br>
<b>Develop the “I Can Handle It”attitude</b><br>
Can you imagine yourself being able to deal with tough situations with grace and 
composure? If you can’t, then get ready to learn this valuable technique. The 
next time you play and are faced with a tough shot or situation, instead of 
worrying about the negative consequences simply say to yourself, I can handle 
it. The power of these four words will lead you to take positive action steps. 
As you are pondering your shot, instead of reacting angrily to the bad lie or 
the anxiety of the moment, tell yourself, I can handle this and walk as if you 
have been through this tough environment many times before. Verbally tell 
yourself, I can handle this situation, and I’ll be successful.<br>
<br>
Research from the field of behavioural psychology suggests that if we behave in 
a manner that promotes confidence, such as walking and talking with 
self-assurance, our feelings and thoughts will start to emulate that behaviour. 
Therefore, if you start to act as if you can handle the situation and move in an 
assured fashion, you are creating a psycho- physiological pathway to composure 
and purposefulness. By acting confident,<br>
you will start to feel and think confidently. In this way, you take control of 
the situation instead of allowing the situation to take control of you.<br>
