                        <b>GSP rates for Switzerland &amp; Turkey</b><br>
                    OCIPED in co-ordination with UNCTAD, Geneva, has compiled 
                    the GSP (Generalized System of Preferences) rates for 
                    Switzerland and Turkey for the benefit of Omani industries. 
                    This is apart from the GSP rates already available for the 
                    EU, the USA and Japan. The expiry date for the GSP scheme of 
                    Turkey is December 31, 2005 and for the<br>
                    GSP scheme of Switzerland is February 2007.<br>
                    <br>
                    The GSP, an initiative of the United Nations Conference on 
                    Trade and Development (UNCTAD), is operated by all major 
                    industrialized nations, known as donor countries, with the 
                    basic objective of increasing the export (foreign exchange) 
                    earnings of developing<br>
                    and least developed beneficiary countries, promoting their 
                    industrialization and accelerating their rate of economic 
                    growth.<br>
                    <br>
                    Under the GSP scheme, any Omani producer, whether involved 
                    in agriculture, industry or handicraft production and able 
                    to comply fully with the relevant terms and conditions, is 
                    able to derive benefit from the GSP scheme, of which Oman is 
                    a beneficiary.<br>
                    <br>
                    However, such benefit is restricted to the product 
                    identified by the donor country to be eligible for GSP 
                    benefit. Currently, the Sultanate of Oman also enjoys GSP 
                    for the EU,<br>
                    Japan and the USA. The GSP scheme of Japan expires in 2011, 
                    the USA GSP scheme in 2006 and the GSP scheme ofEU in 2005.</font></p>
