                    BankMuscat (SAOG) has become the first Omani institution to 
                    go in for a Global Depository Receipts (GDRs) issue. At an 
                    extraordinary general meeting in August, the shareholders of 
                    BankMuscat approved a proposal to allow the bank to increase 
                    its authorized share capital from RO75 million to RO125 
                    million, comprising 125 million shares of a nominal value of 
                    RO1 each. 
                    The bank plans to issue up to 6,878,765 GDRs, which 
                    represents 10 per cent of the bank’s issued share capital. 
                    BankMuscat has obtained all necessary regulatory approvals
                    to go ahead with the proposed issue. <br>
                    <br>
                    Sheikh AbdulMalik bin 
                    Abdullah Al Khalili, Chairman, BankMuscat said: “BankMuscat 
                    has always been open to considering internationally accepted 
                    funding options when seeking to raise funds. The GDR route, 
                    we believe, will allow us to maintain the lowest possible 
                    dilution, while considerably enhancing shareholder value. 
                    This is an important step forward to us both as an 
                    organization that is committed to providing superior returns 
                    to our stakeholders as well as a leading Omani institution 
                    that is fast setting new benchmarks in the international 
                    banking arena.” He added, “We believe this will result into 
                    far greater international investor interest in Oman and its 
                    economy and will also set a trend that many institutions can 
                    follow, resulting in greater inflow of foreign capital into 
                    the country.” <br>
