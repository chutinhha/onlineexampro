            <font face="Verdana" size="4" color="#FF0000">Focused efforts<br>
            <br>
            </font></b><font face="Verdana" size="1"><b>OER traces the aims and 
objectives of the Omani Economic Association, a civil association engaged in 
encouraging scientific research to contribute to the socio-economic development 
activities in Oman<br>
            </b><br>
            <br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="drhatem.jpg" align="left" hspace="5" vspace="2">W</b></font><font face="Verdana" size="1">e 
live in an age of specialization, and specialization requires serious, focused 
efforts. As the world shrinks to the size of a global village, information 
becomes the buzzword for the success for economies. Information is power.<br>
<br>
Information is progress. And information is the mantra of success. But then, 
information has to be organized and structured to tap the dividends. And for 
that to happen, there ought to be bodies that are clear about their objectives 
and the information they want to disseminate, if they want to act as catalysts 
for socio-economic development. <br>
<br>
Oman Economic Association is one such example of focused, systematic and 
integrated effort of an organization. Its basic motto: promoting socio-economic 
development activities in Oman through encouraging scientific research.<br>
<br>
“The basic idea behind the Association,” says Dr. Hatem Al-Shanfari, Member, 
Omani Economic Association, and a faculty at the Department of Economics &amp; 
Finance, College of<br>
Commerce &amp; Economics, Sultan Qaboos University, “is to be able to ensure that we 
sustain our quest for education in an age of specialization.”<br>
<br>
The Board and members of the Association render their services voluntarily with 
no financial compensation and aim at conducting their activities with a 
professional and academic methodology within the framework of the adopted 
objectives as active partners<br>
in the overall development of the nation.<br>
<br>
A legal entity, the Omani Economic Association is registered in the civil 
association registry of the Ministry of Social Affairs, in accordance with 
Ministerial decision No 170/2004 dated 10 October 2004. “It (the Association) is 
a civil society organization in the mould of independent NGOs,” informs Dr. Al-Shanfari.<br>
<br>
“The inspiration for this institution came from the government, which is 
promoting civil societies and organizations,” adds Dr. Al-Shanfari. “This (civil 
societies) is one of the important pillars for modern communities. This option 
was not available to us in the past. Over the last few years, the government has 
really opened up on such issues.”<br>
<br>
How did the idea come up initially?<br>
“We thought it would be very important to link up with people who are 
specialists, specialists, as it would add verve to our Association by helping 
organize our activities in more formal manner. The purpose is to keep in touch 
with members who share the same backgrounds,” the Association member informs.<br>
<br>
Every organization centers round some key objective. “The main objective of the 
association,” says Dr Al-Shanfari, “is to provide services to the members of the 
association for evolving their professional careers; and to provide them a 
platform whereupon they can express their views and look after their interests 
in the community.”<br>
<br>
Does the Association provide expert advice to various agencies in the public and 
the private sector? “At the moment, we are still in our first (foundation) year. 
We are planning to position ourselves so as to provide quality service to the 
community, like certifying economic consultancy firms etc. in the future. We, 
also, hope to work very closely with the government in advising on certain 
issues,” Dr. Al-Shanfari informs.<br>
<br>
<b>Association annual conference </b><br>
The Association intends to hold annual economic conferences. The first Annual 
Conference on the New Economy – aimed at creating awareness on the concept of 
the new economy and diagnosing the challenges that impede the development of 
private sector activity – is slated to be held in Muscat on 1-2 October 2005 
under the auspices of H.H Sayed Asad bin Tariq bin Taimur, His Majesty the 
Sultan’s Representative. The conference aims at highlighting the pivotal role of 
the private sector in economic development and hopes the participants will 
benefit from experiences of other countries and international organizations in 
developing the private sector.<br>
<br>
What is the basic idea behind organizing conferences? <br>
“The main objective is to enrich the members and make them updated with the 
latest trends and issues related to Oman in particular and the global market in 
general. Conferences provide us a chance to interact with reputed scholars, and 
share the richness and wealth of their experience. It’s a means of keeping in 
touch with the latest trends<br>
in economic thinking,” Dr. Al-Shanfari elaborates.<br>
<br>
Is there a move to evolve some special methodology to measure the state of 
economy? “Not really! Not through the conferences,” Dr. Al-Shanfari makes it 
clear. “What we are trying to do is to inculcate a habit of organizing annual 
conferences, to be supplemented by a number of routine activities. For example, 
we had invited distinguished scholars from the Kuwait University to deliver 
lectures on Free Trade Agreements between the GCC and the US. It was a very 
successful event. The lecture was well received by the public.<br>
<br>
“In the future, we plan to organize open forum of discussions between government 
officials and international figures who happen to be in Oman. All this will add 
to the value of the Association,” explains Dr. Al-Shanfari. <br>
<br>
Among the participants in the Association’s conferences are chief administrative 
staff at ministries and government institutions, chief administrative staff from 
the private sector, teaching staff at universities, institutes and research 
centers, and experts in regional and international organizations.<br>
<br>
The participation of experts form such varied backgrounds provides an idea 
platform for exchange of information. <br>
<br>
<b>The Association Awards for Economic Research </b><br>
Then, there are the Association Awards for Economic Research. The Association 
will offer annual awards for the best economic research works, to be delivered 
during its annual conference, with a view to reinforcing and encouraging 
economic research to support the process of development in Oman. The focus of 
research area is supposed to be new applied economic researches and proposals 
for future development in Oman. The research topics include human resource 
development, education and training, development of small businesses, 
entrepreneurs, finance and banking, commerce and industry, transportation and 
shipping, tourism, communication and information technology, health services, 
combat of poverty, social cooperation, assessing government programmes, 
investment environment in Oman, systems and mechanism of government sectors 
functioning, and methods and means of income diversification.The award, it may 
be mentioned, is open to Omanis and researchers from other nationalities. <br>
<br>
The Association also intends to conduct evening sessions throughout the year for 
lectures, symposiums, and workshops on various economic activities. <br>
<br>
How has been the response to the Association’s activities so far? “It has been 
slow,” admits Dr. Al-Shanfari. “But it is natural because people want to see 
more activities. Our public lectures have been very successful. A lot of people 
have participated in the lectures. And the number of registered members has been 
increasing constantly,” informs Dr. Al-Shanfari, with a sense of satisfaction 
clearly emanating from his eyes.</font></p>
