<br>
There were services companies also which couldn’t sustain the profit booking on 
account of Omantel. Given the recent decrease in the share prices due to the 
Omantel listing, which has been more than 10 per cent from the top formed during 
the earlier period, we have tried to gauge whether the downturn has really taken 
off the gains made in the hay days of May-June’05 and if not, what were the 
stocks which were steady. Hence, we had taken two periods viz. <br>
<br>
1) During the brief but huge rally, took the Index up from 4650 levels to the 
high of 5700 levels. <br>
2) During the slowdown prior and after the Omantel listing which brought the MSM 
Index down to the current 5100 levels over a period of nearly two months.<br>
<br>
We have tried to analyze all the listed companies on MSM during the two periods. 
We have sorted on the basis on the gainers during the entire period.<br>
<br>
Gulf International Chemicals has been the largest gainer during the period under 
review, gaining during both the pre and post-Omantel period. Similarly, other 
prominent stocks include Gulf Investment Services, Fincorp, Global Financial 
Investments amongst the brokerage companies in the top gainers, while DIDIC and 
Oman &amp; Emirates, topped the investment sector. <br>
<br>
Banking sector was mixed with Bank Muscat notching the highest overall gain, 
inspite of its faltering down by 17 per cent during the post Omantel deluge to 
still maintain an effective gain of 21 per cent. Similar trend was observed with 
Oman International Bank and Bank Dhofar. However, National Bank of Oman was the 
odd one out with an effective negative gain during the period, inspite of 17 per 
cent during the initial run.<br>
<br>
<b>First Half 2005 Results – Still Making Headlines</b><br>
The corporate performance for the first half of 2005 remained healthy, with the 
overall earnings growth for the entire market growing by more than 30 per cent 
on a YoY basis. The largest gainer amongst the Indices in terms of corporate 
performance has been the banking and investment sector whose earnings have grown 
by a phenomenal 64 per cent on a YoY Basis. Even though, a large part of the 
earnings growth has been due to the holding companies including ONIC, Oman &amp; 
Emirates, DIDIC etc., the banking sector was also not far behind with Oman 
International Bank leading the pack with a 55 per cent jump in its profitability 
on a YoY basis. Following it was Bank Dhofar (26 per cent), Bank Muscat and 
Alliance Housing Bank (around 20 per cent resp.) The industry sector remained 
the second best with a modest 19 per cent increase, largely due to Oman Textiles 
which turned into profitability as against a loss. Other stocks were Oman Cement 
(25 per cent), Al Anwar Holding (50 per cent), Dhofar Cattlefeed (34 per cent). 
However, many of the other indices stocks in the industry faltered in their 
performance, as they couldn’t improve their revenues due to tough market 
conditions. <br>
<br>
<b>Outlook</b><br>
While the oil prices are going higher, the economic conditions are expected to 
become stronger and this favourable orientation would result in good corporate 
performance during the coming period. However, the stock markets at Oman and UAE 
have witnessed some correction taking place subsequent to June 2005 after 
reaching new heights. Such correction, even though difficult to comprehend for 
traders and investors, needs to be swallowed as a bitter pill as it eliminates 
the overweight of certain stocks beyond their fundamentals and ensures a matured 
approach in the stock prices in the long run. We believe that the investors are 
likely to proceed with caution but will be able to maintain their positions and 
interests in the stock market during the balance period in 2005.</font></p>
