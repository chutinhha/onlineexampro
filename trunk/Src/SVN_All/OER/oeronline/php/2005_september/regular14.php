<p align="justify" dir="ltr"><font face="Verdana" size="1"> 
The markets continued to ease further during the period under review (24 July - 
20 August 2005) with very little activity observed in share trading except the 
Omantel scrip. In fact, right from the day of its listing on 28 August 2005, the 
market has been dominated by Omantel trading with almost 90 per cent of the 
value being attributed towards it. And before the share price got into a 
stabilization mode during the last days of the period under review, the 
correction continued to bring the Index down by around 200 points to close at 
5127 levels. Overall, the year to date gains is still at a healthy 52 per cent 
for the Index.<br>
<br>
<b>Omantel – The Buzz Word </b><br>
The traders and investors were all engrossed with Omantel scrip during the 
second fortnight of the period under review. The stock opened with a huge gap on 
its IPO price of RO1.280 at RO2.800, a very high gain of more than 115 per cent 
on its IPO price. However, profit taking took its toll over the next few days 
and along with the reducing volumes, the overall stock price bottomed out at 
around RO1.8, before stabilizing in the range of RO2.150 to RO2.250 (at the time 
of preparing this report).</font></p>
