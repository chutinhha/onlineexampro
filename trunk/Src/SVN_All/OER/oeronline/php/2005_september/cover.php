                now appears to be having more dynamic effects across the region. 
                This reflects a number of key developments, including increased 
                intra-regional capital flows, as reflected in a property boom 
                and a run-up in equity prices; rapid rises in tourism in some 
                countries; and a region-wide acceleration in project 
                implementation. We now estimate that spending on projects this 
                year and next will rise at least fourfold over the $35 billion 
                seen in such investments in 2002-2003.&quot;<br>
                <br>
                Howard Handy, IIF Director for the Middle East and Africa, 
                stated that the IIF anticipates that oil export revenues are 
                likely to rise by 49 per cent to $291 billion in 2005 and inch 
                up to $305 billion in 2006. By way of comparison, revenues from 
                oil exports were just $61 billion in 1998 and averaged under 
                $100 billion for the ten years to 2003. Saudi Arabia's oil 
                export revenue between 2004 and 2006 is forecast to be greater 
                than that for the whole of the 1990s. He said, &quot;Growth in gas 
                exports is even more dramatic, as we projected that export 
                revenue from this source in 2006 ($21.1 billion) will be almost 
                four times greater that the 1994-2003 average.&quot;<br>
                <br>
                Handy added that, &quot;We envisage oil prices, measured in terms of 
                Brent Crude per barrel, averaging about $54 this year and $55 
                next year. Our estimates suggest that even if there is a sharp 
                decline, the GCC governments can cut back on spending and 
                mobilize substantial external assets.&quot; <br>
