                billion in value, and it increased to about $313 billion in 
                2004. Trade between member states has exceeded $21 billion 
                dollars during 2003, up by 20 per cent from the previous year, 
                as a result of the launch of the GCC customs union. Because most 
                GCC member states produce and export oil, there is only limited 
                trade in oil between them. However, the share of intra-GCC trade 
                in the total non-oil trade is quite high, reaching over 40 per 
                cent in some cases. <br>
                <br>
