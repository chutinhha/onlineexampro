                states. Already, in implementation of the Supreme Council 
                resolutions, special attention has been given to non-GCC 
                citizens who have established residency in any member state to 
                travel with very few restrictions between member states. Special 
                rules have also been introduced to facilitate travel by 
                commercial drivers and company representatives within GCC.</font></td>
