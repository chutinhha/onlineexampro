                monetary union. The process should not be revolutionary; instead 
                it should be evolutionary. <br>
                <br>
                <b>… but don’t you think GCC economic integration should be done at 
                a faster pace?</b><br>
                Yes, we need to take more action on this front. But as I said we 
                must take firm steps by learning from the experience of other 
                blocs. We must not repeat the mistakes committed by others. We 
                must move one step at a time. For example, before we accelerate 
                further on economic integration, we must make the customs union 
                work more effectively. The GCC countries should facilitate 
                exchange of goods more smoothly and must refrain from creating 
                any administrative stumbling blocs that affect trade between 
                member countries. <br>
                <br>
                The private sector players in each of the GCC countries should 
                accelerate the process by developing close ties with each other. 
                They should form more joint alliances and invest in other GCC 
                markets to give momentum to the process initiated by the 
                Governments of the GCC countries. They should give constructive 
                feedback on the economic policies to help in ascertaining their 
                effectiveness. <br>
                <br>
                <b>What are the lessons that GCC needs to learn from the experience 
                of other economic blocs?</b><br>
                The lessons are that we should not be emotional in our approach. 
                We should be practical and pragmatic. We shouldn’t allow 
                obstacles to affect the economic integration process. We should 
                look for solutions for any problem. All GCC members understand 
                that more economic integration will serve the interests of their 
                respective national economies. <br>
                <br>
                Therefore, there should be clarity and transparency in their 
                interaction with each other. All of us should take cue from 
                other blocs like the EU and ASEAN. Every member should be 
                treated equally and all of us should respect each other’s 
                interests. <br>
                <b>
                <br>
                What is the role envisaged for Oman in the GCC economic bloc?</b><br>
                Oman has always been very supportive of the GCC economic 
                integration. With our unique location, Oman is a natural gateway 
                to GCC for the outside world. We have been constantly upgrading 
                our infrastructure, including roads network, airports and 
                seaports to facilitate smooth flow of goods from Oman to other 
                GCC countries. <br>
                <br>
                Oman is blessed with relatively more population compared to many 
                other GCC countries. Our recent policy initiatives are targeted 
                at development of human resources on a much larger scale. This 
                will lead to the integration of the labour market in the GCC. 
                Already, a large number of skilled and semi-skilled Omanis are 
                employed in other GCC countries. <br>
                <br>
                <b>Do we give any special preference for importing and exporting 
                goods from GCC countries?</b><br>
                We operate in an open, global, competitive market. We don’t give 
                any special treatment to exports and imports from GCC countries 
                for the sole reason that they are from the GCC bloc. The World 
                Trade Organization regulations don’t permit such liberties. 
                Moreover, the market efficiency demands that we opt for the most 
                competitive option available in the global market.<br>
                <br>
                However, GCC countries are the major trading partners of Oman. 
                Most of Omani products are exported to the UAE and Saudi Arabia. 
                The trade among GCC countries is growing every year. Much of the 
                credit goes to the customs union that has facilitated relatively 
                easy flow of goods between the GCC countries. <br>
                <br>
                <b>What is the impact of GCC economic integration on Omani economy?</b><br>
                We believe that all GCC members are benefiting from the economic 
                integration. Oman is no exception. Take tourism for example. The 
                co-operation among GCC members to facilitate movement of 
                tourists between GCC countries is going to boost Omani economy, 
                as more tourists will come to Oman. <br>
                <br>
                <b>GCC is facing challenges like border disputes or bilateral trade 
                agreements signed by GCC countries with non-GCC countries. How 
                is GCC dealing with such sensitive issues? </b> <br>
                As far as Oman is concerned, we have no border dispute with any 
                country. The issue of bilateral trade agreements has been 
                discussed at various forums within the GCC. And the GCC members 
                have agreed that they can sign bilateral agreements with one or 
                two countries. Some countries had expressed concern that their 
                national interests demand that they sign such agreements. The 
                national interests of a country could also be based on its 
                overall economic, security or strategic concerns besides just 
                trade activities. <br>
                For your information, as a regional bloc, GCC is negotiating 
                with EU, China and Turkey; and signed agreements with Lebanon 
                and Syria. <br>
                <br>
                <b>But don’t you think bilateral trade agreements are going to harm 
                the interests of the GCC bloc?</b><br>
                I don’t think that bilateral trade agreements will harm the GCC 
                bloc. All these agreements are based on specific negotiations 
                and are not done on dictated terms. They take care of the 
                national interests of the involved countries. <br>
                <br>
                For example, our negotiations with China are based on the 
                consideration that China is a big economy with plenty of 
                comparative advantages. Once we mutually agree on negotiated 
                terms, some of the Omani products will get access to the vast 
                Chinese market and similarly Chinese can tap our market for 
                strategic investments and training services. We will try our 
                best to get the best deal for Oman and GCC market from China.
                <br>
                <br>
                <b>In a regional trade bloc, some of the member countries have to 
                sacrifice their national interests in favour of the larger 
                regional interests. How far is Oman prepared to make such 
                sacrifices?</b><br>
                Oman has made sacrifices in the larger interests of the GCC 
                bloc. We started development process quite late in Oman compared 
                to many of our neighbours. We had to forego some of our 
                interests in the short term but we have benefited in the long 
                term. So we are prepared to make the sacrifices but every GCC 
                country has to take its share of the burden.<br>
                <br>
                <b>What sacrifices have been made by Oman for GCC?</b><br>
                We didn’t protect our industries in the beginning. We know that 
                there are other countries in the GCC that are subsidizing some 
                of their industries. May be they are not subsidizing them 
                directly but they have made available utilities like electricity 
                and water at cheaper rates. We can’t afford to do that because 
                we don’t have lot of oil revenue like some other members. We 
                opened our markets to GCC products and our industries suffered 
                for a certain period. But later, we managed to recover and 
                today, we are a tough competitor to beat in our business.</font></td>
