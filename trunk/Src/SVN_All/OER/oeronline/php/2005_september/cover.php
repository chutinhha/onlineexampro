            <p align="justify"><b>
            <font face="Verdana" style="font-size: 20pt">
            GCC <font color="#CC3333">Economic Integration</font></font></b></p>
            <p align="right"><b>
            <font face="Verdana" style="font-size: 14pt"> 
            – A Reality Check</font></b></p>
            <p align="justify"><b>
            <font face="Verdana" style="font-size: 20pt">
            <img border="1" src="gcc.jpg" align="left"></font></b><font face="Verdana" size="1"><b>Gulf Co-operation Council (GCC) has come a long </b></font>
            <font face="Verdana" size="1">
            <b>way but complete 
            economic integration is still a distant dream. Akshay Bhatnagar 
            takes a reality check on the success of GCC so far and the 
            challenges ahead.</b><br>
            <br>
            The Gulf Co-operation Council (GCC) has entered its silver jubilee 
            year in 2005. The GCC, comprising of Saudi Arabia, Oman, United Arab 
            Emirates, Kuwait, Qatar and Bahrain, was born in 1981 out of the 
            sheer need among the six Arab states to preserve their historical, 
            social and cultural bonding. But in the last 24 years, GCC has 
            gradually made giant strides towards marking its authority as a 
            regional economic bloc in the international market. Numbers of steps 
            have been taken to integrate the economies of the six member 
            countries; some initiatives have worked successfully, some have 
            boomeranged before seeing the light of the day and many are in the 
            pipeline. Let’s take a stock of each of them. <br>
            <br>
            <b>Monetary union</b><br>
            The biggest challenge, which the GCC is facing, is on the front of 
            monetary union (MU) of six member countries by 2010. Senior level 
            experts from the six countries are deliberating hard in assessing 
            the pros and cons of the eventual monetary union. <br>
            <br>
            On the positive side, the MU is expected to reduce foreign exchange 
            transaction costs, eliminate exchange rate risk, encourage pricing 
            transparency and lead to more competition. These will encourage 
            trade, investment and growth in the region. “With improved fiscal 
            discipline, inflation and interest rates would go down in the Gulf 
            region. At micro level, the single currency would have a long-term 
            impact on major regional banks by encouraging more efficient use of 
            cash. The costs of hedging against exchange rate volatility would be 
            reduced. The GCC MU would lead to the formation of the largest and 
            most liquid capital market in the Middle East. Portfolio managers 
            and private investors would be able to invest in the region without 
            any fear of additional currency risk,” forecasted a GCC analyst. <br>
            <br>
            But as a consequence of the MU, GCC countries fear that they will 
            lose their national sovereignty by relinquishing their independent 
            control over domestic monetary, fiscal and exchange rate policies. 
            It might also lead to net loss in income, as they will not be able 
            to undertake expansionary monetary and fiscal policies whenever the 
            oil prices go down. Moreover, national budgetary policies of some of 
            the member countries could be subjected to arbitrary restrictions. 
            It could lead to breach of their control over their individual 
            taxation and public spending programs. The GCC countries are still 
            gauging the scale of expected losses and gains out of the MU. <br>
            <br>
            GCC has turned to European Union to advise it on framing up the 
            structure of the MU. Top-level GCC representatives are meeting next 
            year on January 12 to take the MU process forward. The creation of a 
            central bank will also be top of their agenda. The national banks of 
            the GCC countries are already permitted to operate in any part of 
            the region.</font></p>
            <p align="center"><font face="Verdana" size="1">
            <img border="1" src="fig1.jpg" width="291" height="174"></font></p>
            <p align="justify"><font face="Verdana" size="1">
            As the practice followed by all six GCC countries currently, the new 
            single GCC currency (its name yet to be identified) is expected to 
            be pegged to the US dollar. There have been speculations that GCC is 
            reconsidering whether to peg their single currency to the dollar or 
            even link it to a basket of leading currencies, including dollar and 
            euro. But refuting all rumours, Deputy Governor of the Saudi Arabian 
            Monetary Agency, Dr. Mohammed Al-Jasser said: “The GCC has not made 
            any decision yet on pegging the single Gulf currency, which is 
            expected to be put into effect in 2010.”<br>
            <br>
            Some of the economists are pushing for not pegging the currency to 
            any other currencies at all, thus making it a floating currency. The 
            arguments put forth in favour of the floating currency includes 
            reflection of market values, independence in policymaking and 
            insulation from being affected by the negative performance of other 
            currencies.<br>
            <br>
            GCC now needs to accelerate the process of MU as more than two 
            decades have already been spent deliberating on the issue. GCC had 
            initiated the move way back in 1982 when member countries had signed 
            an agreement, which said, &quot;The member states shall seek to 
            coordinate their financial, monetary and banking policies and 
            enhance cooperation between monetary agencies and central banks, 
            including an endeavor to establish a joint currency in order to 
            further their economies.&quot;<br>
            <br>
            The GCC should be able to meet its target of 2010 to make MU a 
            reality as the move enjoys strong political backing. Besides that, 
            compared to other regional economic trade blocs, the six GCC 
            countries share similar macro economic structures; major exports 
            revenue comes from oil and gas sector; and focusing on 
            liberalization and diversification of the economy. On the flip side, 
            despite their best efforts over the years to align their economic 
            policies, the member countries still suffer from a large gap mainly 
            in budget deficits and public debt rates. <br>
            <br>
            <b>Stock market </b><br>
            As expected, the integration of stock markets of the six countries 
            is turning out to be a complex issue. Meeting its deadline of 
            completing the integration process by 2007 seems a daunting task for 
            GCC. As per information available, GCC is considering two options to 
            achieve the integration. The first is to merge all the stock markets 
            into a regional bourse.<br>
            <br>
            And the second is to connect them and set unified rules and 
            regulations to be followed by each of the six stock exchanges, which 
            will make it possible for the citizens of each country to trade in 
            stocks under the same rules. A ministerial level GCC meeting is 
            expected to discuss the issue before it is placed before the supreme 
            council of Gulf leaders for approval. <br>
            <br>
            The process of stock exchange integration is not going to be a 
            smooth process for all the member countries. The stock exchange 
            trading system followed by Qatar, Saudi Arabia, UAE and Bahrain is 
            different from what is being followed by Kuwait and Oman. And there 
            is no uniformity also in the bourses in terms of the cap on foreign 
            investments. As per a stock exchange analyst, “While Qatar allows 25 
            per cent foreign investment in stocks but some countries permit up 
            to 49 per cent and some others none at all. This issue needs to be 
            addressed before the linkage is provided,” said a senior official of 
            Doha Stock Market. <br>
            <br>
            <b>Custom regulations</b><br>
            The unified custom regulation has been one of the most significant 
            achievements of GCC. The process began in 1983 with the 
            establishment of the Free Trade Area (FTA) among the GCC countries. 
            This meant elimination of tariffs on goods of national origins. This 
            single step alone led to a six-fold increase in trade volumes 
            between the GCC countries. Thereafter, in 80s and early 90s, the 
            efforts were made towards reaching an agreement on unified external 
            tariffs to pave the way for GCC Customs Union (CU). In January 2003, 
            the CU was implemented and the six member countries approved that 
            common customs tariff shall be 5 per cent on all foreign goods 
            imported from outside. This has resulted in 20 per cent increase in 
            inter-regional trade volume.<br>
            <br>
            <b>Tourist Visa</b><br>
            GCC is very close to finalizing an agreement on implementing 
            ‘Unified Tourist Visa System’ which means that citizens of many 
            non-GCC countries will be able to travel between the GCC countries 
            by using a single visa issued by any of the six GCC countries. This 
            system is based on the similar visa system ‘Schenghen’ adopted in 
            European Union. The citizens of most of the North American and 
            European states are going to benefit majorly from this easy and 
            hassle free arrangement.<br>
            <br>
            Once implemented, the move is going to further accelerate the 
            tourism inflow in GCC countries as numbers of multi-billion dollar 
            tourism projects are fast developing in the region. <br>
            <br>
            The hospitality sector, including hotels, airlines, travel agencies 
            and tour operators etc., along with retail players, are pressing for 
            its early implementation. “If a business tourist is visiting Dubai, 
            with a unified tourist visa, he can visit Oman to enjoy its tourist 
            attractions in his spare time. He can maximize his gains from the 
            single trip,” said a Muscat based travel agency manager. <br>
            <br>
            As per estimates, the global tourism market is expected to be $300 
            billion and growing at 12 per cent per annum. The move is expected 
            to boost GCC’s share in international tourism, as member countries 
            will benefit from promotional programmes undertaken by various 
            tourism authorities besides presenting the whole region as an 
            integrated tourism destination.<br>
            <br>
            <b>Smart ID cards</b><br>
            GCC General Secretariat has advised its member countries to issue 
            smart cards to their citizens at a faster pace. “We have given 
            member states a deadline before which they should all complete their 
            national identity card schemes. Most of the member states have gone 
            a long way in their programmes and I think they will meet the 
            deadline,&quot; Colonel Rashid Salim&nbsp; Al Muhairi, Director of 
            Security Department at the GCC General Secretariat informed. </font></p>
            <p align="center">
            <img border="1" src="fig2.jpg" width="300" height="210"><br>
            <br>
            <img border="1" src="fig3.jpg" width="300" height="217"></p>
            <p align="justify"><font face="Verdana" size="1">
            The unified smart cards would facilitate easy travel for the GCC 
            citizens irrespective of their nationality within the six GCC 
            countries. Once the unified smart card system is fully operational, 
            GCC citizens will not be required to stand in queue with their 
            passports. With the smart card, they will be able to enter and exit 
            any GCC country easily. The citizens of some of the GCC countries 
            are already traveling in other GCC countries by using their national 
            identity cards under bilateral agreements.<br>
            <br>
            &quot;The smart card will be used as a multi-purpose card. It will be 
            used as an identity card, a driving license, a health card, for bank 
            dealings and at border outlets, etc,&quot; Col. Muhairi added. The size 
            of the card remains same for citizens of all six countries but each 
            country has the liberty to choose its own unique colour (may be from 
            their respective national flags). The card will carry the emblem of 
            the respective country besides the GCC emblem. The expatriates too 
            will be issued the same cards but with certain restrictions.<br>
            <br>
            <b>Railway system</b><br>
            With railway networks getting preference over road transportation 
            world over, GCC member countries have also taken up a project ‘Arab 
            Railway Network Project’ which is aimed at connecting them through 
            an extensive railway network The rail network is going to increase 
            the exchange of goods and trade activities in the region and make it 
            easy for the GCC citizens to move from one country to another. <br>
            <br>
            While the process for the final planning of the project is still 
            going on but most of the member countries have agreed in principle 
            to support the project. In the past there had been few oppositions 
            to the project over its impact on the environment, especially to 
            marine and wild life. But keeping in view the long-term economic 
            benefits of the project, GCC is looking at it on a positive note. A 
            special committee comprising of GCC representatives is conducting a 
            study to evaluate the feasibility of the project. The railway 
            network would further increase trade activities in the region. <br>
            <br>
            <b>Power grid </b><br>
            GCC is expected to begin the construction work on the first phase of 
            the multi-billion dollar GCC power grid project in September this 
            year. The project aims at interconnecting Saudi Arabia, Kuwait, 
            Bahrain and Qatar to reduce the cost of generating power. The GCC 
            Power Grid Authority, made up of representatives from all Gulf 
            countries, is based in Dammam. <br>
            <br>
            As per a GCC Secretariat statement, “The four Gulf states - Saudi 
            Arabia, Kuwait, Bahrain and Qatar - will be interconnected by the 
            year 2008; the whole grid project will be completed by 2010. The 
            appointment of a panel to supervise the GCC power grid project was 
            the final step toward project implementation, and that means a big 
            step forward for economic integration throughout the Gulf region. 
            Upon the completion of phase one, the total cost savings rate of 
            return after four years from operations could be $2.5 billion.&quot; <br>
            <br>
            The huge power-grid linkage project is expected to save the Gulf 
            countries billions of dollars through efficient management of power 
            production and distribution. The idea to set up a central power grid 
            is significant as the investment in linking of the GCC power grids 
            would be less than establishing and maintaining multiple power 
            generation plants in individual countries in the region. This will 
            also ensure regular power supply to the industrial units mushrooming 
            all over the Gulf countries, thus leadi<img border="0" src="fig4.jpg" width="177" height="111" align="right">ng towards boosting their 
            industrial productivity. The GCC countries raking in surplus power 
            will be able to sell it to those countries, which are suffering from 
            power shortages. <br>
            <br>
            With infrastructure development in full swing in the GCC region, the 
            demand for power is growing at an alarming rate. Some of the 
            industrial zones and residential townships are facing low occupancy 
            on account of shortage of power. In the Kingdom of Saudi Arabia 
            alone, the power consumption is growing at a rate of 5.5 per cent 
            annually. And as per market estimates, the Kingdom needs to invest 
            $375 billion in the next 20-years to meet the rise in its power 
            consumption. </font></p>
