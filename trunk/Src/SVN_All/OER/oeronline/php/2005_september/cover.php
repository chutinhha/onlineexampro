                infrastructure development projects in GCC?</b><br>
                Differences of opinion over details of borderlines between 
                neighboring countries are common all over the world. Our member 
                states have been working hard to define these technical details 
                of their borders with their neighbors, whether GCC member states 
                or not. This process takes time and it does not affect the 
                brotherly relations between our member states, which share a lot 
                more than geography. At the same time, the GCC as an 
                organization is a proof that our member states are committed to 
                full integration and removing or reducing all barriers that 
                could delay this integration.<br>
                <br>
                <b>What steps have been taken so far to establish GCC power grid?</b><br>
                The GCC heads of state adopted the power grid project and 
                authorized the creation of the Power Grid Organization, a 
                specialized agency of the GCC that works under the supervision 
                and guidance of GCC ministerial committees. The organization, 
                which is based in Dammam, Saudi Arabia, is fully functioning. 
                Briefly, the project has two phases: the first is intended to 
                connect Bahrain, Saudi Arabia, Qatar and Kuwait, and the second 
                will connect Oman and the UAE to the other four. Both groups are 
                currently making great efforts to reach the level of readiness 
                needed for the grid to be completed.<br>
                <br>
                <b>How far has the GCC moved in the direction of a single currency 
                by 2010? </b> <br>
                The GCC Supreme Council has approved a clear timetable to 
                achieve a monetary union and launch a unified currency by 
                January 2010. The ministerial committees have adopted interim 
                deadlines to achieve that and we are on schedule. According to 
                the timetable, GCC member states are to reach agreement on 
                “convergence criteria” for the unified currency. We already have 
                agreement in principle on these criteria. As you know, agreement 
                on these criteria is a crucial point in any monetary union, as 
                was clear in the history of the European Monetary Union. As for 
                the anchor of the unified currency, no decision has been made on 
                that regard. In the meantime, GCC currencies have all being 
                pegged to the US dollar as a way of fixing their cross exchange 
                rates.<br>
