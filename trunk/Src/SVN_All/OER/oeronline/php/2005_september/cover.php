            <b>Traffic law</b><br>
            The GCC member countries are very close to adopting a unified 
            traffic law in the next three years. A unified GCC traffic law was 
            proposed a few years back but GCC members were not able to reach a 
            consensus on the final plan. But the draft has been revised and 
            submitted again for consent from the member countries. In fact, some 
            of the countries have already incorporated certain clauses from the 
            earlier draft in their local traffic laws. <br>
            <br>
            Once implemented, the new unified traffic law is expected to reduce 
            the road accidents rate. As per the new law, a GCC citizen will not 
            be required to pay traffic fine in a member state other than his 
            home country. For example, if an Omani citizen commits a traffic 
            violation in the UAE, he can pay the fine in Oman later on. In a 
            recent meeting of the GCC, the officials also discussed phasing out 
            leaded petrol by 2007.<br>
            <br>
            <b>Roadblocks</b><br>
            An important issue, which creates tension among the member 
            countries, is political in nature. It relates to the border 
            disputes. The failure of the respective governments to resolve their 
            border disputes leads to either delay or non-implementation of major 
            regional infrastructure development projects like bridges, railway 
            network and electricity grids. Many a time the benefits of a project 
            in one country fail to touch the lives of citizens of its 
            neighbouring countries due to the prevailing border tensions. <br>
            <br>
            Despite making striking gains in the recent past, GCC has tough task 
            up its sleeves, as member countries have not been able to completely 
            resolve their differences, which are affecting the progress of the 
            economic integration front. Take the case of customs union. The 
            customs union agreement suffered a jolt when the UAE refused to sign 
            an accord barring bilateral trade deals except with the United 
            States. While one section of the GCC believes that bilateral 
            agreements violate the GCC tariff pact, the other section believes 
            there should be some flexibility to sign bilateral agreements with 
            non-GCC countries in their strategic national interests. Many of the 
            GCC countries are negotiating for such bilateral agreements with 
            many countries. As per sources, Qatar is in talks with Singapore, 
            Oman is negotiating with India and the UAE is looking at a deal with 
            Australia.<br>
            <br>
            A policy mechanism needs to be worked out to avoid such kinds of 
            disputes on a permanent basis rather than dealing with them on a 
            case-to-case basis. <br>
            <br>
            Despite differences on some of the issues related to taking GCC 
            economic integration forward, GCC member countries are most likely 
            to achieve monetary union by 2010 and resolve most of the matters 
            related to it. <br>
            <br>
            History has proved that countries with similarities in their 
            economic structures and systems are more likely to succeed as a 
            unified economic bloc. Most of the GCC countries have similar 
            economies as oil exports are their main source of revenue and they 
            all want to diversify to cut their dependence on oil. All the six 
            GCC countries follow the same religion, language and traditions. 
            They have enjoyed the inherited wealth so far but it’s time to 
            jointly build the evolutionary wealth! </font></p>
