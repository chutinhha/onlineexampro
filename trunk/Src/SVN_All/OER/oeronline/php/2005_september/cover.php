                <br>
                <b>How is the GCC shaping up as an economic bloc? </b> <br>
                Economic integration between GCC member states is moving ahead 
                steadily according to clear timetables and workable programs. 
                This solid economic base has made the GCC a viable and vigorous 
                economic bloc. The integration process is moving along all key 
                tracks: the customs union, the common market, the monetary 
                union, as well as the harmonization of trade, industry, 
                development and oil policies. <br>
                <b>
                <br>
                What is the combined share of GCC countries in the world trade?</b><br>
                As for GCC share in the world economy, and despite the GCC’s 
                small size in terms of total population, its GDP is now over 
                $400 billion, more than half the GDP of the region. Its trade 
                with the rest of the world is in excess of $300 billion. As an 
                indicator for its integration in the world economy, GCC trade as 
                share of GDP is close to 80 per cent, one of the highest rates 
                in the world.<br>
                <br>
                <b>What are the major imports and exports of GCC region? </b> <br>
                The main GCC exports are obviously oil, gas and petrochemicals, 
                which account for over 90 per cent of GCC exports. Some of our 
                member states also export aluminum, fish and agricultural 
                products, but these are small compared to oil and gas exports.<br>
                <br>
                <b>What are the unique features of GCC’s unified tariff system? How 
                is it benefiting the member countries? </b> <br>
                The main features of the GCC customs union, which was launched 
                in 2003, include a low and simple external tariff of 5 per cent 
                for most goods. The only exception is some 417 basic goods, 
                which are exempted from customs tariffs. Our member states 
                agreed to this relatively low rate in order to benefit the 
                citizens and residents of the GCC on the one hand, and encourage 
                trade with the rest of the world. By removing high tariffs, the 
                GCC member states demonstrated their willingness to encourage 
                trade with the rest of the world. <br>
                <br>
                <b>How does the GCC plan to meet threats like some GCC member 
                countries signing bilateral trade deals with non-GCC countries?</b><br>
                The GCC member states are determined not to let bilateral trade 
                agreements interfere with the smooth working of the GCC customs 
                union and are working to make them consistent with its 
                requirements.<br>
                <br>
                <b>On the smart card front for citizens, what is the deadline for 
                the GCC countries to issue the cards and what are going to be 
                its benefits? </b> <br>
                When the idea of the smart card was approved in the May 2004 
                consultative meeting of the GCC heads of state, the member 
                states were given two years to complete the process of adoption 
                of the card. Preparations are well under way in all member 
                states to issue the smart card. The smart card will make it 
                easier for GCC citizens to benefit from the GCC common market 
                agreement, which makes it possible for them to move easily and 
                reside in all member states on equal footing, as well as engage 
                in most economic activities and take advantage of social, health 
                and educational services. <br>
                <br>
                <b>What progress has been made on the creation of the railway 
                network connecting GCC countries?</b><br>
                The GCC railways network is in the study phase. An initial 
                pre-feasibility study has shown that the project is worthwhile. 
                A full feasibility study is currently going on. This is the 
                stage we are now in. After the economic feasibility is 
                completed, member states will decide on the next steps. In their 
                latest meeting in June 2005, GCC ministers of transport 
                authorized the Saudi Arabian Ministry of Transport to prepare a 
                request for proposal document for the study while a technical 
                committee of GCC experts has been formed to supervise it.<br>
                <b>
                <br>
                What are your estimates of oil revenues of the GCC countries in 
                2005? How is it going to impact the strengthening of GCC 
                economic bloc? </b> <br>
                I will leave the estimates of oil revenue this year to the 
                experts, especially since we still have several months to go 
                before the end of the year, but it is clear so far that oil 
                revenues this year are going to be quite high. All GCC member 
                states have very ambitious projects, some under way, to channel 
                the extra revenues in order to strengthen their economies. This 
                will certainly provide great opportunities for the private 
                sector, both national and foreign, to carry out these plans.<br>
                <br>
                <b>What is the combined capitalization value of GCC stock markets 
                as of now? What are your plans to integrate the stock markets of 
                six GCC countries?</b><br>
                GCC stock markets are growing by the day. As you probably know, 
                the total capitalization increased by over 400 per cent over the 
                past four years. At the beginning of this year, it exceeded $530 
                billion. Since then, it has grown by nearly 50 per cent. GCC 
                countries have some of the largest stock markets in the region. 
                A study had been commissioned by the GCC for the further 
                development and integration of GCC stock markets. The study was 
                completed recently by an international consulting group and a 
                technical committee has been meeting to formulate 
                recommendations to achieve the goals.<br>
