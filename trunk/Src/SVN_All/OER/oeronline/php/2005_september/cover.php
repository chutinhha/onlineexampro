                trade zones etc. with other regional trade blocks such as 
                European Union?</b><br>
                With the launch of the customs union and unification of customs 
                rules and regulations in GCC member states, trade negotiations 
                between the GCC and its trading partners should move more 
                quickly and smoothly. The GCC has collectively concluded 
                economic cooperation and trade agreement with several trading 
                partners, including China, India, Pakistan, Turkey, and the 
                Mercosur Group (Mercosur is a regional trading bloc formed in 
                March 1991, consisting of Argentina, Brazil, Paraguay and 
                Uruguay. In 1996, Chile and Bolivia joined as associate 
                members). We have recently signed FTA agreements with Lebanon 
                and Syria. As for the European Union, which is our number one 
                trading partner, we are near to signing a full free trade area 
                agreement, to replace the economic cooperation agreement the two 
                sides signed in 1988.<br>
