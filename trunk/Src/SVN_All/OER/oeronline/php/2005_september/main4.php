idea of a nation state is “dysfunctional” when considering the flow of economic 
activity, according to global strategist Dr Kenichi Ohmae, who points to a 
“region state” as a more appropriate unit of measurement.<br>
<br>
Ohmae, who is one of the nine speakers at November’s international leadership 
summit, Leaders in Dubai, said: “Policymakers, politicians and corporate 
managers would benefit from looking at ‘region states’ - the globe's natural 
economic zones - whether they happen to fall within or across traditional 
political boundaries. <br>
“With their efficient scales of consumption, infrastructure and professional 
services, region states make ideal entryways into the global economy.”<br>
<br>
Referred to as 'Mr. Strategy', Ohmae has an unsurpassed reputation as an advisor 
on global strategy to foreign governments and multinational corporations. The 
Financial Times referred to him as &quot;Japan's only management guru&quot; and The 
Economist has referred to him as one of the top five management gurus in the 
world.<br>
<br>
Ohmae has written more than 140 books, including The Borderless World, first 
published in 1990, which predicted the rise of globalisation. In fact, Ohmae 
coined the word there for the first time.<br>
<br>
He said: “The nation state has become an unnatural, even dysfunctional, unit for 
organising human activity and managing economic endeavour in a borderless world.
<br>
<br>
“It represents no genuine, shared community of economic interests; it defines no 
meaningful flows of economic activity. In fact, it overlooks the true linkages 
and synergies that exist among often disparate populations by combining 
important measures of human activity at the wrong level of analysis.”<br>
<br>
Ohmae gives the example of Italy: to consider it as a single economic entity 
ignores the reality of an industrial north and a rural south, each vastly 
different in its ability to contribute to the economy. Treating Italy as a 
single economic unit, forces corporations to operate on the basis of false 
averages, as the country has great disparities in industry and income across 
regions. <br>
<br>
In comparison, Ohmae puts forward his solution of a region state. He says: 
“Region states are natural economic zones. They may or may not fall within the 
geographic limits of a particular nation - whether they do is an accident of 
history. <br>
<br>
“Sometimes these distinct economic units are formed by parts of states, such as 
those in northern Italy, Wales, Catalonia, Alsace-Lorraine or Baden-Württemberg. 
At other times they may be formed by economic patterns that overlap existing 
national boundaries, such as those between San Diego and Tijuana, Hong Kong and 
southern China, or the &quot;growth triangle&quot; of Singapore and its neighbouring 
Indonesian islands. <br>
<br>
“In today's borderless world, these are natural economic zones and what matters 
is that the possesses, in one or another combination, the key ingredients for 
successful participation in the global economy.”<br>
<br>
Ohmae will be putting forward his ideas on the dynamics of the regional state at 
