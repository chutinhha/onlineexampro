What makes a good business plan?<br>
<br>
</font><font face="Verdana" size="1"><b>Tim Berry prescribes the key to making a 
perfect business plan and cautions against the common mistakes made while 
chalking out business planning</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>
<img border="0" src="manag.jpg" align="left" hspace="5" width="120" height="141">C</b></font><font face="Verdana" size="1">ome 
September and top managers are scampering to take forward the business planning 
to avoid last minute glitches. But what makes a good plan? Is it the length of 
the plan? The information it covers? How well it's written, or the brilliance of 
its strategy. No.<br>
<br>
You can think about the good or bad of a plan as the plan itself, measuring its 
value by its contents. There are some qualities in a plan that make it more 
likely to create results, and these are important. However, it is even better to 
see the plan as part of the whole process of results, because even a great plan 
is wasted if nobody follows it.<br>
<br>
A business plan will be hard to implement unless it is simple, specific, 
realistic and complete. Even if it is all these things, a good plan will need 
someone to follow up and check on it. The plan depends on the human elements 
around it, particularly the process of commitment and involvement, and the 
tracking and follow-up that comes afterward. <br>
<br>
Successful implementation starts with a good plan. There are elements that will 
make a plan more likely to be successfully implemented. Some of the clues to 
implementation include: <br>
1 Is the plan simple? Is it easy to understand and to act on? Does it 
communicate its contents easily and practically? <br>
2 Is the plan specific? Are its objectives concrete and measurable? Does it 
include specific actions and activities, each with specific dates of completion, 
specific persons responsible and specific budgets? <br>
3 Is the plan realistic? Are the sales goals, expense budgets, and milestone 
dates realistic? Nothing stifles implementation like unrealistic goals. <br>
4 Is the plan complete? Does it include all the necessary elements? Requirements 
of a business plan vary, depending on the context. There is no guarantee, 
however, that the plan will work if it doesn't cover the main bases.<br>
<br>
<b>Use of Business Plans</b><br>
Too many people think of business plans as something you do to start a company, 
apply for a loan, or find investors. Yes, they are vital for those purposes, but 
there's a lot more to it.<br>
Preparing a business plan is an organized, logical way to look at all of the 
important aspects of a business. First, decide what you will use the plan for, 
such as to: <br>
• Define and fix objectives, and programs to achieve those objectives. <br>
• Create regular business review and course correction. <br>
• Define a new business. <br>
• Support a loan application. <br>
• Define agreements between partners. <br>
• Set a value on a business for sale or legal purposes. <br>
• Evaluate a new product line, promotion, or expansion.<br>
<br>
No Time to Plan? A Common Misconception<br>
“Not enough time for a plan,” business people say. “I can't plan. I'm too busy 
getting things done.” A business plan now can save time and stress later.<br>
Too many businesses make business plans only when they have to. Unless a bank or 
investors want to look at a business plan, there isn't likely to be a plan 
written. The busier you are, the more you need to plan. If you are always 
putting out fires, you should build firebreaks or a sprinkler system. You can 
lose the whole forest for too much attention to the individual trees.<br>
<b><br>
Keys to Better Business Plans</b><br>
• Use a business plan to set concrete goals, responsibilities, and deadlines to 
guide your business. <br>
• A good business plan assigns tasks to people or departments and sets 
milestones and deadlines for tracking implementation. <br>
• A practical business plan includes 10 parts implementation for every one-part 
strategy. <br>
• As part of the implementation of a business plan, it should provide a forum 
for regular review and course corrections. <br>
• Good business plans are practical.<br>
<br>
<b>Business Plan “Don'ts” </b><br>
• Don't use a business plan to show how much you know about your business. <br>
• Nobody reads a long-winded business plan: not bankers, bosses, nor venture 
capitalists. Years ago, people were favorably impressed by long plans. Today, 
nobody is interested in a business plan more than 50 pages long.<br>
