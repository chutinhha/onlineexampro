<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">GONDP: 
Juicing up sweet success</font></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="oncp.jpg" align="left" hspace="6" width="146" height="153">Oman 
National Dairy Products Co. Ltd. (ONDP), an Omani joint stock company (WJ Towell 
&amp; Co.: 31%; Ominvest: 20%), is a 27-year-old organisation that manufactures 
dairy and juice products. It was the brainchild of a visionary Omani 
entrepreneur who recognised the potential for establishing a dairy for the 
purpose of catering to the country's demand for dairy products. This vision 
germinated with the birth of ONDP, which, at that time, was one of the first 
enterprises of its kind in the region. From a humble beginning, it gradually 
grew and expanded its product portfolio by acquiring sophisticated world-class 
manufacturing technology. <br>
<br>
At the time there was little local competition while production capacities in 
other parts of the region were barely sufficient to meet the demands in their 
respective countries. Oman was considered a distant outpost with a vast 
geographic terrain across which was spread a small population. Distribution 
costs were (and remain) among the highest in the region. In such a relatively 
sheltered environment, ONDP moved from strength to strength and established a 
reputation for being one of the world-class manufacturers of juice and dairy 
products in the region. <br>
<br>
In the last decade, however, the business environment changed with accelerating 
rapidity and became increasingly complex. With trade barriers dropping by the 
day, international brands started flooding the market. Regional brands that were 
hitherto content with meeting the demands of their respective domestic markets 
found that growth was steadily tapering off on account of increasing local 
competition. They began focusing on Oman, a market they had until now ignored, 
and deployed resources to aggressively enter this market. The burgeoning 
competition eroded ONDP's market share. Besides, competitive price pressures led 
to declining margins. <br>
<br>
The downward slide was arrested to a large extent four years ago. Since then, 
ONDP has undergone a considerable amount of change that has resulted in a huge 
turnaround of performance. How did this happen? In over 70 per cent of cases, 
organisational change occurs in difficult times. It was no different at ONDP. 
Culture: Given that organisational culture is the key factor that can determine 
success or failure of any change program, the ONDP management initiated an 
internal study to look beneath &quot;the way we do things here&quot;, surface the 
prevailing culture in the organisation and identify the gaps so as to be able to 
close them and pave the way for change. <br>
<br>
During this process, the management team revisited ONDP' s prevailing vision, 
mission, core values, objectives, the strategy derived thereof and their 
relevance in the current context. As a result of this exercise, ONDP was able to 
fine-tune these elements and adopt a focused strategy for growth. <br>
<br>
Any change involves a change of mindset among employees, either specific groups 
or organisation-wide, depending upon the scale of change. In order to get them 
to embrace change, employees must first buy into the reasons for change, or else 
they may lapse into a state of cognitive dissonance (a state that arises when 
people find that their beliefs are inconsistent with their actions). Therefore, 
effective communication is a key element of change. Departmental heads at ONDP 
were assigned the task of drilling the company's raison d'etre, vision, mission 
and core values down the line. Consequently employees at all levels were made 
aware of what their organisation was all about, their roles and what was 
expected of them. The role of coaching was given top priority. <br>
<br>
Reporting structure: The reporting structure was revamped in keeping with the 
rejuvenated strategy. For instance, Exports, which was earlier a part of the 
overall Sales &amp; Marketing function, was hived out as an independent entity 
reporting directly to the GM in keeping with its enhanced importance to the 
strategy for growth. Similarly, Supply Chain was attached to Factory management, 
thereby ensuring smooth flow of material, both inbound as well as outbound. As a 
result of these changes, processes were streamlined and workflow smoothened. <br>
<br>
Management and operational processes: Given that authority and accountability go 
hand in hand, the company's Schedule of Authority was updated and simplified. 
The installation of BaaN Enterprise Resource Planning system revolutionised 
operational processes and information flows. This in itself was a huge culture 
change, be it with regard to processes, training, employee acceptance or 
implementation. <br>
<br>
ONDP's focus on quality and food safety was further strengthened through the 
process of obtaining ISO and HACCP accreditation. It is one of the first 
companies of its kind in the region to achieve these standards. <br>
<br>
Measurement procedures: A tool for measuring employee performance became 
available in ONDP with the introduction of a Performance Management System 
(PMS). Under this system, every single employee signs off on set of measurable 
objectives at the beginning of the appraisal period. Performance reviews are 
conducted half-yearly. Annual performance is reviewed at two levels Ð by the 
immediate superior as well as his manager. An employee's performance against his 
objectives determines his increments, promotions and bonuses. <br>
<br>
Besides being an effective mechanism for role clarity and employee feedback, PMS 
is the foundation for a merit-based reward system at ONDP. Consistent role 
models: Last but not the least, in ONDP's change process, unit heads were 
encouraged to 'walk the talk' and be consistent role models for their 
subordinates. This, in fact, was incorporated as one of the Key Performance 
Indicators in their performance objectives.<br>
<br>
In conclusion, developing new strategies or operational initiatives is the most 
important way companies renew themselves, by helping to preserve their 
competitive advantage and stimulating platforms for long-term success. Preparing 
the company for a change by making any level of the organisation better able to 
deal with it may be as important as the details of the project. At the same 
time, incremental change has more chance of success as opposed to change of a 
radical nature. </font></p>

