<p align="justify"><font face="Verdana" size="4" color="#FF0000">The eclectic 
one</font><font face="Verdana" size="1"><br>
<br>

</font><b><font face="Verdana" size="1">Akber Mustafa A. Sultan, the managing 
director of Mustafa Sultan Enterprises, divulges the various electrifying shades 
of his persona to Vicky Kapur in a no-holds-barred tête-â-tête</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="profile.jpg" align="left" hspace="5" width="175" height="191">Hailing 
from the proprietary Sultan family did not earn him a licence to begin his 
career at the top of the organisational hierarchy, as is the case with some of 
the other born-with-a-silver-spoon tycoons. Akber M.A. Sultan, the son of 
Mustafa Sultan, had to rub shoulders with his company's drivers and salesmen and 
prove his mettle before he was inducted into the management cadre, and is today 
the managing director of Mustafa Sultan Enterprises. <br>
<br>
Born in 1962 in Mattrah, Akber Sultan is the third generation of the Sultans in 
the family business, which was set up by his grandfather Abdullah Sultan in 
1886. &quot;All the four generations since have been involved in the family 
business,'' he informs. The 'business,' which actually started off as a 
merchandise shop in Mattrah, is today a diverse conglomerate with turnover 
running into millions of riyals Omani. &quot;We used to trade food items, perfumery, 
medicines, car spare parts, building material etc. at that time,'' he maintains.
<br>
<br>
Akber Sultan, whose siblings include two brothers and two sisters, finished his 
secondary schooling from the Islamic Institute of Secondary Education, and then 
graduated in Business Administration (Finance) from the California State 
University in Sacramento (US) before joining the family business. &quot;I graduated 
in December 1986, and joined the family business soon after in 1987. The group 
was then known as Mustafa Jawad Trading Company, a JV between my father and my 
uncle, who inherited the business from my grandfather,'' he informs. <br>
<br>
<b>Childhood memories</b><br>
&quot;I was a very shy boy,'' insists Akber Sultan. &quot;I was not into books at all. I 
hated reading books because I used to get bored,'' he adds. As a child, he was 
more interested in sports, and used to play a lot of table tennis and soccer. &quot;I 
used to play table tennis at our home, where we had bought a table. During that 
time, there were only two urban areas - Mattrah and Muscat. Darsait, Ruwi etc. 
were all rural areas. We used to play in farms in Ruwi,'' he says. <br>
<br>
While his childhood was normal, Akber Sultan faced his biggest challenge when he 
grew up a little and went to the university in the US. &quot;That was a horrible 
experience,'' he shrugs. &quot;I had to stay all alone - cooking, washing and all, 
and I had no one to talk to,'' he exclaims. Coming from a large family, that 
must have been quite an adjustment to make. &quot;I used to just listen to the TV,'' 
he remembers. &quot;After the classes, I used to take a nap for an hour, and then 
from 5 to 9, I used to go the university library and study. After that, I used 
to go back to my room, have dinner and then sleep.'' <br>
<br>
Being shy by nature, he did not make too many friends on the campus. In fact, he 
did have some relatives staying a few hundred miles away from the campus, but 
going to them would have been like accepting defeat, which he could not afford. 
So, he laboured on and emerged winner with his degree in end-1986. <br>
<br>
<b>Learning the ropes </b><br>
&quot;When I came back after completing my studies, I had no experience about 
business. I was a freshman,'' he maintains. &quot;So, my father suggested that I join 
Capital Stores as a distribution manager, reporting to the general manager, who 
in turn reported to the group general manager and so on so forth.'' This in 
effect meant that he started off at the junior-most level of the chain of 
command. &quot;The job involved a lot of movement in the market. I spent time with 
the company's drivers and salesmen,'' he maintains. <br>
<br>
His big moment arrived in 1990, when the organisation's group general manager 
left, and his father invited him to join at the group level. &quot;At that time, one 
of my brothers was in the office equipment division and the other was 
studying,'' he says. After the partial separation of the group took place in 
1991, Akber Sultan shifted from Capital Stores to head companies that came under 
the banner of Mustafa Sultan Enterprises as group GM. Since then, the group has 
blossomed under his stewardship. <br>
<br>
<b>Awards and accolades </b><br>
Mustafa Sultan Enterprises has won various awards apart from the facilitations 
received from various local and foreign agencies under his stewardship. The 
company's lighting unit has won the best agents worldwide awards in 1991-92 
besides receiving a letter of appreciation from PDO for prompt completion of the 
Fahud Airport lighting project. <br>
<br>
The consumer electronics division of the company won the Philips Consumer 
Electronics Excellence in Best Business Practice award in 2000. The office 
technology entity bagged the Best Distributor for Gulf region award from Mita 
Corporation in 1997 and the Best Distributor award from Kyocera Mita in 2000.
<br>
<br>
<b>Group structure </b><br>
Akber Sultan manages the Telecommunications division of the organisation, which 
is further divided into Infrastructure Projects, Service Provider &amp; Services, 
and Distribution (mobiles) units. Apart from that, he also looks after the 
franchisee shop, which is in partnership with Phono of Dubai. The other 
divisions that he looks after directly are Electronics, and Mustafa Sultan 
Exchange. <br>
<br>
One of his brothers, Shabir Sultan, looks into Office Equipment, Lighting, and 
Restaurants businesses while Abdulredha Sultan, the other brother, looks after 
the Science &amp; Industry, Security &amp; Communication and Real Estate businesses. 
&quot;One of my two sisters, Noor Jahan, is also in family business - as commercial 
manager for the group, looking into the Pay TV business,'' he adds. <br>
<br>
<b>Management mantra</b><br>
&quot;Like any other family business in the Gulf, we are on our way to migrating from 
a family-run business to a professionally-run one,'' avers Akber Sultan. &quot;I 
won't say that we have reached that level, but we are aiming to reach there very 
soon,'' he admits. &quot;All three of us brothers believe in managing professionally 
and delegating responsibility to the professional managers and general 
managers,'' he adds. <br>
<br>
According to him, &quot;in any organisation, people come first. That's the unwritten 
mission statement of our organisation too. We consider all employees as our 
business partners, and go out of the way to help them.'' This, according to him, 
not only increases their productivity, but also loyalty for the group. &quot;We have 
had many instances where people--both Omanis and expatriates--have quit the 
company only to return to its folds within a year or two. And we don't mind 
taking them back,'' he confesses.<br>
<br>
The organisation also devotes adequate attention to customer satisfaction. &quot;We 
have forgone profits a lot of times to attain customer satisfaction. Business 
has to be fair to the investor, the customer and the employees at the same time. 
If one of them is not satisfied, the triangle is wrecked,'' he promulgates. &quot;For 
our customers, we have at times replaced products that haven't been replaced by 
our principals in turn,'' he claims.<br>
<br>
<b>Challenges &amp; competition </b><br>
&quot;Our business has seen a lot of ups and downs, not necessarily along with the 
highs and lows experienced by the country,'' he admits. &quot;Sometimes, it's one's 
own inefficiency that leads to slower than expected growth. So, we have to 
improve constantly, and move towards professionalism,'' he acknowledges. &quot;I 
think businesses in Oman have to stop thinking as merchants/traders and start 
acting as entrepreneurs. The biggest challenge is in changing the people's 
psyche. We must make investments in HR, training, and IT to accustom people to 
the new working style.'' Regarding his organisation in particular, Akber Sultan 
claims that the &quot;challenges are more in terms of selling items in which we 
cannot add much value to the final product. These are challenging because 
anybody can get these items from across the border, probably at a lower cost 
because they won't be incurring marketing or stocking expenses. Besides, price 
deterioration in these items is very rapid.'' <br>
<br>
&quot;However, if you move towards value-added service-driven products,'' he adds, 
&quot;the challenge comes only from local competitors - not individuals. So, one has 
to move into items towards the higher end of the price spectrum, which are 
difficult for people to import randomly as they are risky and expensive - for 
instance Plasma TVs, LCDs etc.''<br>
<br>
&quot;Moreover, we add value to the product in terms of offering credit facility to 
the consumer, service warranties, free installation and servicing of the 
equipment etc. At times, we also offer promotions for interest-free instalments. 
Besides, the consumer can be sure of the genuineness of the product when buying 
from us,'' he adds. <br>
<br>
<b>Personal profile</b><br>
&quot;I got married in 1989, and have four children - two boys and two girls,'' tells 
Akber Sultan. &quot;Three of them are studying while one is an infant.'' He spends 
most of his free time with his family and friends. Every fortnight, he spends a 
day in his farmhouse in Al Khoud along with his family and some very close 
friends, which are &quot;like family.'' <br>
<br>
His favourite &quot;urban'' holiday destination is London, where he and his family 
enjoy activity-filled vacations while for a quiet, leisurely break, he prefers 
the Australian coast. &quot;Switzerland too qualifies as one of my favourite 
vacations haunts,'' he claims. Akber Sultan is not too much of a TV-watcher. &quot;I 
watched all the TV I had to watch during my lonely days in the university,'' he 
jokes. &quot;Now, I just watch news on TV. Besides that, I spend my free time by 
browsing the Internet, reading, exercising and socialising.''<br>
<br>
<b>Finishing touches </b><br>
For the young Omanis who want to become entrepreneurs and those who want to 
diversify their existing businesses, Akber Sultan has a word of advice: &quot;The 
best way to become an entrepreneur or grow your businesses is to acquire 
technical expertise of people who are already in the business through management 
contracts.'' That, according to him is not only safer, as the formula has 
already been developed and tested by somebody else, but also prudent since one 
does not have to reinvent the wheel and incur additional expenses in doing so.
<br>
<br>
He is one of the few entrepreneurs who practise what they preach. For evidence, 
look no further than his portfolio, which comprises of the X-change maestro card 
(management contractor: State Bank of India); City Exchange in Dubai (State Bank 
of Travancore); Phono Shop (a joint venture, for which technical support comes 
from the principal); and a Lebanese restaurant (a joint venture managed by the 
partner who specialises in hotel management). <br>
<br>
&quot;I tried this method and found it extremely successful. Even in the future, I'll 
go in for the same formula, banking on foreign experience. So, if it works for 
us, I see no reason why it wouldn't work for others.'' Mark his words.</font></p>

