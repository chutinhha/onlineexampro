            <p align="justify"><font face="Verdana" size="1">A positive side to the new legislation is that it would decongest 
            some crowded areas as remote areas would come to inhabited by 
            expatriate real estate buyers. It would also check remittances 
            expatriates sent abroad, as they will have an avenue to invest money 
            back in Qatar itself. Qatar Real Estate Investment Company Projects 
            Manager Ali E. Ahmed believes that the boom would not reverse 
            anytime soon. ''Massive investments are being made in various 
            sectors and there is constant recruitment happening. For a long time 
            to come, there will be need for both residential and commercial 
            properties. The boom is only beginning here.'' <br>
            <br>
            Qatar's largest real estate project--the Pearl of the Gulf--has 
            already attracted investors although the promoters, United 
            Development Co. (UDC), are yet to start marketing it. The $2.5 
            billion (RO962 million) project has attracted interest not only from 
            Qatar and the GCC, but from the West as well. The project will be 
            built 350 metres offshore close to the West Bay lagoon area of Doha. 
            It is the state's first development to offer freehold and 
            residential rights to the international community. <br>
            <br>
            The Pearl of the Gulf will cover 400 hectares of reclaimed land and 
            house some 30,000 people in a multi-cultural residential and 
            commercial community. According to UDC Special Projects Manager 
            Salim Abdul-Rahim, by the end of 2006, the first tenants will be 
            able to move in. ''The man-made island project was launched only in 
            April this year, and will incorporate the creation of a vibrant 
            residential and commercial community.'' <br>
            <br>
            The island will have 10 distinct districts, each with an 
            architectural theme, and residential development will include a 
            variety of low-rise, exclusive villas, stylish town houses, 
            condominiums and luxury penthouses. UDC managing director Khalil 
            Sholy says that the road show to promote the project will begin 
            later this year and will cover the US too. ''The difference between 
            us and others is the environment in which we operate. Qatar is a 
            booming economy and we are part of that. Investing in the Pearl of 
            the Gulf is investing in Qatar.'' <br>
            <br>
            Several banks and financial institutions have been vying with each 
            other to invest and participate in the project, says Sholy. The 
            project will come up 350 mt offshore close to the elite West Bay 
            lagoon area of Doha. But experts are confident that unlike certain 
            other GCC states that are either nearing saturation or are already 
            experiencing downslide, Qatar is still in the nascent stages. <br>
            <br>
            In 2003, the sector grew at the rate of 3.35 per cent at current 
            prices as compared to a negative growth of 3.13 per cent in 2002. 
            The sector represented 7.4 per cent of total GDP, making the third 
            largest contribution to the total GDP among non-oil and gas sector 
            in 2003. The level of public expenditure is a very important factor 
            impacting the growth of this sector in the economy. Due to the 
            realisation of budgetary surpluses in the last four fiscal years, 
            the level of public spending has also increased. <br>
            <br>
            The public projects in sectors such as health, education, housing 
            and infrastructure have been allocated a huge QR8.8 billion (RO931 
            million) in the 2004-05 budget. The number of building permits 
            issued also increased significantly by more than 19% in 2002, as 
            compared to single digit growth in the earlier years. The number of 
            building permits issued grew to 4,237 in 2002 from 3,556 in 2001.
            <br>
            <br>
            Qatar is witnessing an unprecedented boom in the real estate sector 
            and there is an increase in investment opportunities in the 
            construction sector. The economic boom and the increase in 
            population have resulted in the rents going up by 25 to 30 per cent. 
            Currently, the demand for residential units and hotel rooms has far 
            exceeded the supply. One of the major real estate projects in Qatar 
            include the Al Dafna Residential Towers which will be constructed by 
            the Thani bin Abdulla Housing Group and will cost around QR600 
            million (RO63.46 million) with a total built up area of 303,364 sq 
            mt. It will comprise of 1,593 units, two- and three-bedrooms, all 
            support facilities and with flexibility in the design can be 
            converted into 5,000 hotel bedrooms and junior suites. <br>
            <br>
            The company has targeted to incorporate this development into the 
            Asian Games as non-athlete accommodation to maximise the rate of 
            return. For the Asian Games 2006, new hotels and residences will be 
            constructed to accommodate 10,000 athletes and officials, 10,000 
            non-athletes and estimated 150,000 spectators. After the games, 
            developers with flexible complexes such as Al Dafna Towers will be 
            able to adjust to the market conditions.<br>
            <br>
            Among the other major developments in the Qatar real estate sector 
            include a project to build a new multi-purpose complex comprising 
            covered markets, conference halls and theatres at a total cost of 
            more than QR700 million (RO74.03 million). <br>
            <br>
            The real estate, construction &amp; housing sector will continue to 
            witness boom due to the increased allocation by the government in 
            budget 2004-05 for public projects such as health, education, 
            housing and infrastructure which will fuel the economic activities 
            in almost every sector. These will increase the demand for the 
            housing sector as well. Apart from this several projects are coming 
            up in the country that will increase the labour demand and, 
            therefore, the demand for the housing sector. Asian Games effect on 
            tourism and new construction will also add to the demand. Therefore 
            the outlook for the sector will remain bright at least for the next 
            2-3 years. <br>
