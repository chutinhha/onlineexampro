            a finger in every pie, Qatar has also jumped onto the real estate 
            bandwagon, with a recent decree allowing expatriates and foreigners 
            ownership rights at select projects in the country. Even without the 
            freehold law, the country has been experiencing a real estate and 
            construction boom, with commercial and residential towers 
            mushrooming all over the capital city Doha. <br>
            <br>
            <img border="0" src="im21.jpg" align="left" hspace="5" width="250" height="151">As it cements its position as the richest Arab country, and with the 
            upcoming Asian Games in 2006, Qatar's boom is set to continue for a 
            while. With expansions in both the energy sector and other areas of 
            diversifications like education, sports and health, there seems to 
            be an insatiable need for more properties. The recently passed law 
            gives further fillip to this real estate boom. As per the law, non-Qataris 
            can buy and own real estate in Pearl of the Gulf, West Bay Lagoon 
            and Al Khor Resort projects. <br>
            <br>
            Non-Qatari buyers could own real estate at the above three locations 
            for 99-years and the period would be extendable for a similar period 
            (99 years). They could also own one or more residential units in the 
            above residential complexes on a 99-year basis. Meanwhile, housing 
            rents in Doha have shot up to unprecedented levels, with even the 
            Ministry of Economy issuing a warning that such uncontrolled hikes 
            could adversely affect the economy and increase inflation. <br>
            <br>
            Local businessmen, however, hope that the new law allowing 
            expatriates to own properties will bring down spiralling rents. 
            Qatar's already booming real estate market may witness added 
            buoyancy once the law is enforced as bigger residential projects 
            would be launched and the shortage of housing stock would end. 
            According to one businessman, Saudi businessmen who were here during 
            an official visit recently had shown a keen interest in investing in 
            the real estate market in Qatar. Such investors from neighbouring 
            Gulf countries would now be inspired to launch big construction 
            projects in our country, he added. ''I see huge investments flowing 
            into the Qatari real estate sector after this law is implemented,'' 
            he said. </font></p>
            <p align="center">
            <img border="0" src="im22.jpg" vspace="3" width="200" height="129"><br>
