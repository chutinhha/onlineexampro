            <font face="Verdana"><font size="4" color="#FF0000">Entrepreneurship 
            in Business: <br>
            </font><font color="#FF0000" size="2">CAN IT EXIST?</font></font><p align="justify"><font face="Verdana" size="1">
            <b>An entrepreneur adds value and creativity to the resources 
            available and exploits the market place to grow business. On the 
            other hand, a manager’s prime focus is to take care of the 
            resources. Can managers turn into entrepreneurs? Professor 
            Shailendra Vyakarnam, faculty member of MCE, answers</b><br>
            <br>
            <img border="0" src="im2.jpg" align="left" hspace="5" width="312" height="182">Taken 
            from the point of view of economists, we can either define the 
            entrepreneurship process as one where the entrepreneur creates a gap 
            in the market through invention, or else fills a gap in the market 
            through trading. In the former case, the entrepreneur is thought to 
            create disequilibrium in the market because the invention or product 
            does not exist and its arrival disrupts the status quo. In the 
            latter, the entrepreneur identifies an opportunity for profit by 
            spotting a gap in the market for a product or service and fills the 
            void through trading arrangements, bringing about equilibrium in the 
            market.<br>
            <br>
            Both processes need multiple competencies to make them happen. These 
            might include technical, marketing and financial skills, as well as 
            the crucial abilities to recruit and retain talented entrepreneurial 
            people. In other words, entrepreneurship is plural. Hence the 
            emerging notion of the entrepreneurial team and the movement away 
            from the mythology of the 1980s, when individualism was celebrated 
            without deeper consideration of the nature of entrepreneurship. <br>
            <br>
            Another key factor about entrepreneurship is that it occurs over 
            time. It takes a certain period of time to develop critical mass, 
            market credibility, knowledge about customers and suppliers and how 
            they operate. Having considered processes, let us take a look at the 
            people behind the process. <br>
            <br>
            <b>Who, then, is the entrepreneur? </b><br>
            This is one of the most difficult and, some feel, wasteful questions 
            in management, as the characteristics described by much of the 
            research in the area could equally apply to other styles of 
            management. <br>
            <br>
            Our interest should be in whether or not we can identify behaviours 
            that are entrepreneurial and how, if at all, we can capture this for 
            the benefit our business. Edith Penrose provided us with the very 
            useful resource-based theory that helps to define the difference 
            between the entrepreneur and the manager. The former she defines as 
            someone who adds value and creativity to the resources available and 
            begins to exploit the market place to grow the business. On the 
            other hand, the manager's prime focus is to take care of the 
            resources. <br>
            <br>
            If we accept this basic difference between risk-taking entrepreneur 
            and resource-controlling manager, then we have to consider how the 
            process of entrepreneurship can be adopted in existing businesses 
            employing people whose prime function is not to risk resources. <br>
            <br>
            <b>Innovation, entrepreneurship and management <br>
            </b>Innovation, especially that which is externally facing in the 
            form of product and service development for business growth, is a 
            creative process that challenges the status quo in the company. 
            Every new product or service needs either to add to or replace an 
            existing product. There are thorny questions about the nature of the 
            products and services, how to establish strategic fit, allocate 
            productive resources for their exploitation and manage the impact 
            that new things will have on what currently exists. <br>
            <br>
            When construed from the perspective of innovation as a management 
            challenge, we limit our possibilities. When we see innovation from 
            an entrepreneurship perspective, we may well open up new ways of 
            looking at the challenge facing us. Let us look at this proposition.</font></p>

                    <p align="justify"><font face="Verdana" size="1">
                    Venture creation as a process can be seen in terms of 
                    garnishing resources to exploit an opportunity. In plain 
                    English, this means developing a network of contacts who can 
                    open up sales possibilities, provide low-cost services, 
                    answer questions about legal and other matters and find 
                    people who can make things happen. In a start-up, these 
                    issues begin to get answered when the entrepreneurial team 
                    seeks out contacts, gets on the phone, and makes 
                    presentations to venture capitalists, potential clients and 
                    others. <br>
                    <br>
                    In contrast, managers in larger and older organisations 
                    manage according to established rules and precedents - 
                    which, over time, creates a comfort zone. Entrepreneurs 
                    discover what has to be done while making the journey. They 
                    have to accept, however, the importance of the managerial 
                    role, frustrating though this may be. Hence the need for 
                    multiple competencies - plural entrepreneurship. There are 
                    some inherent tensions in these propositions, which, if not 
                    resolved, will become stalling points for the business. <br>
                    <br>
                    <b>Do we have the culture for melding entrepreneurship with 
                    management? </b><br>
                    Some of the more recent work at Transitions has brought 
                    together issues of innovation and entrepreneurship with that 
                    of generating an enabling culture to make things happen. We 
                    draw on the work of anthropologists and sociologists who 
                    have framed four propositions: <br>
            </font><font face="Marlett" size="1">
                    h</font><font face="Verdana" size="1">Where there is little 
                    individual freedom and a strong sense of group belonging, we 
                    observe hierarchies. In such organisations, we find tall 
                    command units, with little scope for cross-fertilisation and 
                    networking. They are good for crisis management and 
                    operational efficiencies, but not for mental exploration, 
                    ideas generation, networking and business effectiveness in 
                    changing market environments. <br>
            </font><font face="Marlett" size="1">
                    h</font><font face="Verdana" size="1">Where there is little 
                    individual freedom and no real sense of belonging to a 
                    group, we find an environment of fatalism. The fragmentation 
                    of human talent in such an environment does not allow for 
                    team-based, or plural, entrepreneurship. <br>
            </font><font face="Marlett" size="1">
                    h</font><font face="Verdana" size="1">With increased 
                    individual freedom and little sense of belonging, we 
                    envisage individualism, living up to the conventional, if 
                    misinformed, stereotypes of entrepreneurs. They have energy 
                    and a strong sense of survival, but are uncomfortable in 
                    team settings. They are good for challenging the status quo 
                    and for turnaround management, but not for sowing seeds for 
                    the long term. Many sales functions and trouble-shooting 
                    internal consultants conform to this behaviour. <br>
            </font><font face="Marlett" size="1">
                    h</font><font face="Verdana" size="1">Finally, we have a 
                    group that is typified by high levels of individual freedom, 
                    but within a group-belonging context. Here we have an 
                    egalitarian structure, most typically in knowledge-based 
                    organisations. Networking, creativity and teamwork are 
                    valued norms. Their weaknesses can be lack of clarity and 
                    leadership, resistance to authority and structure, and an 
                    insular group culture that stresses functional rather than 
                    cross-functional working. <br>
                    <br>
                    The operational impact on achieving innovation can be 
                    affected by the dominant cultural configuration in the 
                    business. An essentially hierarchical, rational structure is 
                    not likely to provide the necessary headroom for the 
                    individualist to function in entrepreneurial ways. 
                    Fatalistic situations too are going to militate against the 
                    creation or exploitation of opportunities through lack of 
                    resources and critical mass. Thus, in situations where there 
                    is individual freedom, and structures permit the movement 
                    between high and low interaction with others, we find 
                    environments that most enable corporate entrepreneurship to 
                    function.</font></p>

