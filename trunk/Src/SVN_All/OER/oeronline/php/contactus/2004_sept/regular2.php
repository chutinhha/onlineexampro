<p align="justify"><font face="Verdana" size="4" color="#FF0000">Fraud 
prevention</font><font face="Verdana" size="1"><br>
<br>
</font><b><font face="Verdana" size="1">
<img border="0" src="im44.jpg" align="left" hspace="5" width="150" height="184">Ali 
Hassan Moosa provides an overview of the fraudulent activities associated with 
the global banking industry and the preventive measures being taken</font></b><font face="Verdana" size="1"><br>
<br>
Two decades ago, fraud threat was technically 
unsophisticated, practically unorganised and, in many parts of the world, 
locally focused. Today, however, organised criminal groups using highly 
sophisticated technology mostly control fraudulent activity across the world. It's daunting to note that fraud threats are not just limited to ATM and credit 
cards - all delivery channels and bank products may be subject to fraud. <br>
<br>
The 
vast majority of bank frauds can be referred to as 'white-collar' crimes. 
White-collar crimes are non-violent illegal acts requiring certain degree of 
knowledge and expertise in the field. Bank related fraud might take different 
shapes and forms, such as: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Cheque fraud and kitting:</b> Cheque fraud is a huge 
problem for banks across the world. It involves issuing cheques without 
sufficient funds, stopping legitimate payments (post cheque issuance) through 
stop payment instructions and kitting (money-in-transit). An example of kitting 
is where a fraudulent cheque is deposited into an account, and before the bank 
collects the money, the non-existent money is cashed or transferred into a 
different account. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Credit cards:</b> Credit card fraud has caused enormous losses 
for credit card companies, banks and customers alike. The vast expansion of 
credit card usage across the world has dramatically increased fraud activity. 
Deception techniques include skimming of customer/ account details and unauthorised usage. 
<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Loan application fraud:</b> Even in the Sultanate, there have 
been isolated incidents where certain individuals have misrepresented their 
financial strength, collateral values and credit history in order to get credit 
facilities. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>ID fraud:</b> Some of the most popular bank fraud techniques are 
through identity theft. There have been a number of reported incidents across 
the region where individuals have faked their IDs either through the use of 
someone else's ID or through carefully manipulated photocopied IDs. <br>
<br>
Stringent 
internal controls and processes as well as an effective legal framework should 
reduce the risk of such bank frauds. Know Your Customer (KYC) guidelines issued 
by regulators must strictly be followed to minimise potential losses. Credit 
card issuers started using intelligent technologies to detect spending patterns 
in order to identify strange behaviour and take remedial action even before the 
reporting of a credit card fraud. Cheque fraud can be reduced through the use of 
counterfeit technology such as watermarks and high-resolution micro-printing. 
Using hardware-based Personal Identification Number (PIN) encryption techniques 
(a security standard that is already adopted by banks in Oman) can reduce ATM 
fraud. <br>
<br>
Furthermore, FIs should keep a close watch on 'red flags' or suspicious 
activities. Such actions fall into four broad categories: <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Suspicious 
transactions:</b> Examples include frequent large cash deposits and withdrawals, 
suspicious documentation, customer refusing to explain the purpose of a 
transaction, customer being controlled by another person during transaction 
request, and customer being unconcerned about heavy transaction charges. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Suspicious customer behaviour:</b> Examples include nervous customer behaviour, 
customer asking about internal procedures, customer depositing dusty or dirty 
money, customer not counting deposited or withdrawn cash, and customer refusing 
to continue a transaction after being told that it must be reported or a higher 
level authorisation is required. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Suspicious customer identification:</b> Examples 
include customer refusing to provide a valid ID, customer providing unusual IDs, 
customer address being outside the country or unreachable, no proper telephone 
numbers provided, and business customers refusing to furnish full picture of 
their activities. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> <b>Suspicious employee behaviour: 
</b>Statistics reveal that 60 per 
cent of all fraud is the result of employee involvement. Examples include 
employee avoiding taking vacations, employee providing inaccurate credentials, 
employee involved in recurring exceptions, employee being party to both front 
and back-end transactions, employee overriding internal controls, and employee 
living a lavish lifestyle, neither reflecting the compensation package nor 
family circumstances. <br>
<br>
One of the most serious white-collar crimes is money 
laundering. Crime prevention agencies around the world estimate money laundering 
related activities to be annually in excess of $500 billion (RO192 billion). 
<br>
<br>
Money laundering itself is a process in which the objective is to hide the 
illegal origin of the source and ownership of money. The origin of 'dirty' money 
is not restricted to drug trafficking, but also to financing of terrorist 
activities, arms smuggling, tax evasion, embezzlement, and bribery. <br>
<br>
On a 
micro-level, damage to the reputation of a financial institution that is linked 
whether proactively or passively to money laundering activities may have dire 
implications on that institution and its stakeholders. Once the integrity of a 
financial institution is in question, its long-term viability is at risk, with 
wider economic consequences on the environment in which it operates. <br>
<br>
On a 
macro-level, money laundering has wider global implications specifically for 
under-developed countries where money launderers use such countries because of 
the less regulated environment as a base for their illegal activities, adversely 
affecting the development and prosperity of these economies. <br>
<br>
To combat this 
enormous problem that was threatening the integrity of some global financial 
institutions and governments alike, the Financial Action Task Force (FATF) was 
formed by the G7 countries (US, UK, Germany, France, Italy, Japan, and Canada) 
in 1989 and was charged with the objective of laying down ground rules for 
governments to help crack down money laundering. <br>
<br>
Currently, there are 31 
countries as well as the European Commission and the GCC that have adopted the 
40 recommendations to combat money laundering issued by the FATF. Many other 
countries are in the process of complying with these recommendations. <br>
<br>
Due to the 
negative affect of money laundering on the macroeconomic stability and 
sustainable growth, the International Monetary Fund (IMF) and the World Bank 
carry out regular assessments based on best international practices. 
Furthermore, the Bank of International Settlement (BIS) had issued important 
guidelines relating to due diligence and Know Your Customer (KYC). Local 
regulators around the globe were encouraged to use such standards and 
appropriately adopt them to their environments. <br>
<br>
The Sultanate has come a long 
way in setting up a well-regulated environment that seriously combats money 
laundering. The Central Bank of Oman (CBO) had issued a number of circulars to 
local financial institutions, starting back in 1991 where it had instructed 
banks and other financial institutions in the Sultanate to adopt measures 
recommended by the FATF. <br>
<br>
The Capital Markets Authority (CMA) is also proactively 
involved in educating publicly held companies and assisting them in the set-up 
of prevention guidelines. Moreover, the Royal Oman Police (ROP) formed a 
specialist unit for combating financial abuse. <br>
<br>
The most important step in 
combating money laundering in the Sultanate came in 2002, through the issuance 
of a royal decree no. 34/2002 that defined the legislative framework and laws 
and regulations for combating money laundering. One of the noteworthy points in 
the decree was the requirement for financial record-keeping up to 10 years, well 
above the FATF requirement of five years.<br>
<br>
Regulators around the world are 
demanding proactive measures to guard against financial crime. With the expanded 
use of the Internet and the increase of online financial transactions, the 
financial industry has become more vulnerable to fraud. As online transaction 
capabilities expand, so do the cleverness and capabilities of criminals, making 
it imperative that financial institutions remain on top of criminal techniques 
and remedies. As criminals become more sophisticated, external risks to 
financial intermediaries will become as much of a threat as internal ones. <br>
