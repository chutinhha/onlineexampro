                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Feeling Insecure?</font><p><font face="Verdana" size="1"><b>Many companies in the Sultanate are finding how vulnerable they are to hacking to protect their business reputation. Experts say fears overs security and the number of local attacks are on the rise. OER reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2001_march_april/oer2.jpg" align="left" hspace="5" vspace="5" width="125" height="249"></font><font face="Verdana" size="2">The delay in introducing full e-commerce and Internet banking services in the Sultanate of Oman (and, indeed, the Gulf region) is partly due to a lack of local confidence by the banking, finance and retail sectors which remain doubtful of the level of security that can be provided to local customers (see OER's Banking &amp; Finance supplement). According to industry experts, the lack of data on the subject is hindering efforts to combat the problem.<br>
                    <br>
                    "Accurate statistics are very difficult to obtain. It is certainly true that security breaches through the Internet attract much more publicity than more traditional security breaches away from the e-commerce which still appear to account for the vast majority of actual financial losses," said Helmut Byloff, an e-security manager with Accenture. "There is certainly no room for complacency, especially from local companies. Effective security is a key part of the e-commerce infrastructure."<br>
                    <br>
                    Internet security analyst Michael Knights (see page 22) conducts research for the International Centre for Security Analysis that focuses on which organisations and businesses are likely to be targeted by cyber-criminals. He says the larger and more successful an organisation is, the greater the risk of attack. "Stealth - that is, maintaining a low-visibility presence on the Internet - is the best defence against attackers, but this approach is often not possible and not desirable by businesses that wish their products and services to be bought and widely recognised."&nbsp;<br>
                    <br>
                    Attackers are attracted to a target in two ways. The first is through the use of an automated scanning tools that scans for a particular vulnerability which the attacker aims to exploit. Other types of scanning tools may search for names or domain name components of targets that the attacker wants to hit. If your business name is similar to a prominent target, it may be attacked by accident. Similarly, your organisation may be targeted because of a distinctive feature of your unique reference locator (URL) or internet address. "This occurred frequently during recent Pakistani attacks on Indian sites, when organisations based in the US state of Indiana were mistakenly targeted," says the e-threat analyst.<br>
                    <br>
                    Alternatively, when hackers wish to simply hit as many sites as possible in one attempt, they may try a 'server hack' in which they target an Internet Service Provider (ISP) and therefore all the sites hosted by the ISP. Such hacks aim to 'enslave' the computers on the network for use in the future with these computers acting as 'zombies', each attacking when commanded to by the hacker," he adds. OER contacted a technician at Oman Telecommunications Co. who said that Omantel had experienced problems with such types of attack and reported that such breaches of security were on the increase but declined to comment further.<br>
                    <br>
                    Accenture's Helmut Byloff told OER that security is not the main concern of businesses who are opting for fast growth, but uncertainty on whether the investment will offer significant return. He says only the companies with the deepest pockets and the best business models will be the eventual winners in e-commerce, and those which will be able to afford high level security and have the foresight to implement the best defense at the right time.<br>
                    <br>
                    "We see the primary concern among business leaders in the Gulf to be the lack of a compelling business case to support the level of investment required to attain a leadership position in the market. This uncertainty centres around the level of Internet penetration and, more significantly, the level of business and corresponding revenue streams that can be generated. This is a major factor in the trend towards forming alliances and consortiums to address the opportunities presented within a reasonable<br>
                    risk framework," added Byloff.<br>
                    <br>
                    The advent of Wireless Application Protocol (WAP) in the Gulf throws up a number of questions relating to whether the new technolgy proclaimed as the mobile Internet will be more susceptible to misuse and WAP-hacking. "First generation WAP phones do not, in general, provide for secure communications. However, the new generation will offer custom security solutions through advanced SIM cards and will support generic security architectures such PKI, digital signatures, Smartcards and Wireless Transport Layer Security thus providing substantially enhanced levels of security," added Byloff.<br>
                    <br>
                    <b>Risk of attack in different sectors of the economy&nbsp;</b><br>
                    <br>
                    "While automated targeting procedures like those described above account for the majority of hacks and especially the majority of hacks against small dot-coms and personal sites, some larger sites are more systematically targeted. Government, criminal intelligence and military sites (especially those bearing the prestigious .gov and .mil domains) are always high-risk targets, as are law enforcement sites, because they represent authority and a challenge. However, multinational corporations are also high-profile targets, particularly when they have been criticised on environmental, animal rights and ethical issues. The petrochemical, pharmaceutical and finance and banking sectors are particularly prone to attacks from hacktivists. Such attacks may relate to the company's policies or the policies of one of its subsidiaries or investments," emphasised Knights.&nbsp;<br>
                    <br>
                    <b>Five tips to protect your Company from hackers today</b><br>
                    <br>
                    1. Change passwords for business e-mail and website access often and keep them private;<br>
                    <br>
                    2. Remove bank account details or other valuable financial data on a computer that is connected to the Internet;<br>
                    <br>
                    3. Employ a hacker to test the level of security you currently have - treat this as part of your company's risk-assessment;&nbsp;<br>
                    <br>
                    4. Implement a basic level of computer safety through the use of firewalls and intruder detection systems;&nbsp;<br>
                    <br>
                    5. Do not use general off-the-shelf computer security software.&nbsp;<br>
                    By the summer of 2001, Lucent Technologies expects to spin off its microelectronics business, which includes the optoelectronic components and integrated circuits (IC) divisions, into a separate company that is the world's leading provider of communications semiconductors. An initial public offering of the new company, Agere Systems, is expected to take place by the end of the first quarter. Besides this, Lucent is busy concentrating on global expansion. OER asked Ken Vargo, Lucent's Gulf business development director, for his views on Internet security among other issues.<br>
                    <br>
                    <b>What priorities do you find are high up on the agenda of local IT businesses?<br>
                    </b><br>
                    "There are quite a few user-oriented concerns that affect new e-commerce businesses;&nbsp;<br>
                    <br>
                    The one issue that dominates all the others is the consumer's need for ease of use;&nbsp;<br>
                    <br>
                    The number two issue is access, a crucial element to ensure the success of internet-based business;&nbsp;<br>
                    <br>
                    The third issue is security, especially payment security;&nbsp;<br>
                    <br>
                    The fourth issue is customer assistance. Customers, and especially new customers, need to be able to get help while using the new electronic commerce. Linking e-commerce business to a call center where a customer receives help from a human takes much of the fear out of electronic transaction.&nbsp;<br>
                    <br>
                    These four items are vital to creating a 'Brick 2 Click' success story.<br>
                    <br>
                    <b>What e-commerce growth does Lucent foresee for the region?</b><br>
                    <br>
                    "Lucent has headquarter offices in Cairo (serving the Mediterranean), Riyadh (serving Saudi Arabia) and the UAE (serving the Gulf region, including Oman, the U.A.E., Bahrain and Qatar). Lucent's business expansion curve in the region continues to steadily increase at double digit rates and has for the past five years. Our business increases in direct proportion to the rate at which telecommunications service providers offer new and advanced services such as optical and digital networks and cable television. Capital is being invested by the local ISPs to build new and improve communication infrastructures for Gulf residents at a fantastic rate. Lucent is a builder of networks and therefore as the service providers grow, we do."<br>
                    <br>
                    <b>Why are we continuing to see dot-com failures?&nbsp;</b><br>
                    <br>
                    "The stock market is a tricky and complex investment vehicle. The issue for dot-coms is what value do they bring to the table? Dot-coms are at the beginning of their growth cycle. The biggest impediment to the success of dot-coms is the growth in the Internet access. Local Internet Service Providers (ISPs) are working hard to provide Internet access for everyone. The explosive Internet growth rates are not limited to the US and Europe. Oman is also showing an incredible increase in users for Internet services. As more people in the Gulf region gain access to the Net, more dot-coms will develop offering products to meet the needs of the consumer. We, at Lucent, are seeing many global companies establish new offices in the Gulf region to give the consumer the added value of their services in an e-commerce medium."</font></p>
