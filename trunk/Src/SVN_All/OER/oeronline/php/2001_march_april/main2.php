                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Retail
                    Therapy</font><p><font face="Verdana" size="2"><img border="0" src="../../images/2001_march_april/oer3.jpg" align="left" hspace="5" vspace="5" width="200" height="106"></font><font face="Verdana" size="1"><b>City Centre Muscat is taking shape and aiming to market aggressively when it opens in October. OER looks at Muscat's rapidly growing retail sector. Nayl D'Souza reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    There it is, close to Seeb International Airport, crouched beside Sultan Qaboos Highway: A sprawling shopping complex seemingly in the middle of nowhere. What you are looking at is the new City Centre Muscat, an RO20 million project set to open in October. Promoters of the City Centre project say the anchor store will attract at least three to four million customer visits per year for tenants.&nbsp;<br>
                    <br>
                    UK and local company Tarmac Alawi are handling its construction while the overall development is being financed by Dubai-based business tycoon Majid Al Futtaim who is personally overseeing the project. He is the major shareholder of the UAE's Al-Futtaim Group, a large diversified holding company organised into five divisions: automotive, electronics, insurance, real estate and retailing sectors. Besides the Sultanate, it also has subsidiaries and joint ventures operating in Egypt, Qatar and Pakistan.&nbsp;<br>
                    <br>
                    "Located just five minutes from Muscat's airport in the fast growing district of Seeb, the centre is perfectly located to service the population with a shopping, leisure and entertainment experience under one roof," said Andrew Keegan, senior property development manager for Majid Al Futtaim. "At the moment, we are two or three weeks ahead of schedule, so an October opening is written in stone," he said.&nbsp;<br>
                    <br>
                    The developer's pre-feasibility study shows that around 180,000 people reside within 10 minutes drive of the City Centre Muscat. By 2010, the population within this drive time is expected to increase to 370,000. Moreover, City Centre Muscat's catchment area (the area within half an hour's drive of the shopping complex) provides a target market of 500,000 people.&nbsp;<br>
                    <br>
                    "The centre is located at the crossroads of a potential one million customers in the Greater Muscat and Batinah regions. We aim to provide easily accessible dominant retailing for the increasing local residential population as well as to become the destination of choice for regional residents," he added.&nbsp;<br>
                    <br>
                    Leasing commitments will be finalised with local retailers interested in setting up shop in the City Centre Muscat on April 15 while the handover date of these outlets to successful applicants is June 1. The anchor outlet will be a Carrefour hypermarket, (French company Carrefour is one of the world's largest hypermarket chains), the shopping center will house a leisure and entertainment zone called Magic Planet, a selection of restaurants and over 60 retail outlets including an additional six sub-anchor stores. OER understands that a number of local companies holding international franchises are interested in the six sub-anchor units which include brands such as Body Shop, MacDonalds and Early Learning Center. An application has also been received by Gulf Imports Est., a local company that has so far introduced a portfolio of top British, European and North American brands including Next, Milano, BHS, Mothercare and Claire's Accessories in Qurum.<br>
                    <br>
                    Vicki Senior, operations manager for Gulf Imports Est., said she is impressed with the high visibility and strategic positioning of the shopping centre: "We are seriously looking at a possible expansion in the City Centre Muscat. Muscat and Salalah are both small markets but we would like to possibly duplicate the well-known brands that we already have established in Qurum but within a larger selling space in order to offer a wider selection to our customers."&nbsp;<br>
                    <br>
                    Gulf Imports Est. has the opportunity to introduce other well-known brands which it feels could enjoy sizeable demand during the next five years in Oman - including Topshop, Topman, Wax Lyrical, Elena Miro and Prinicples. Next and Milano were launched in the Sultanate in November 2000, while BHS and Mothercare were introduced back in 1983.<br>
                    <br>
                    "In Oman, we are targeting a teenage market with increasing disposable income. The growing 17-25 age-group are going to be increasingly important for us in the future. It is a market that has existed for a few years and we are confident we will tap this market and cater for families from baby clothes to ladies and children too," she said.&nbsp;<br>
                    <br>
                    Local banks are also interested in the Muscat City Centre. "We have expressed a firm interest in the Muscat City Centre and have already submitted an application for tenancy," Said Ahmed Al Badai, assistant general manager retail banking of Bank Muscat told OER. "We need to extend our services and we are very impressed with the opportunities it could bring to us."<br>
                    <br>
                    "If our bank had a branch and other facilities in the Centre, we would be able to supply all the tenants with a point of sale and there would be an opportunity to do business with over 60 shops including Carrefour," said Khalfan Rashid Al Tal'ey, OIB's assistant general manager and head of retail banking, who also noted that OIB is looking closely at linking up with the retail development constructed by Ajay Enterprises, a large shopping pad being developed at Mawaleh Roundabout, in view of the City Centre Muscat.<br>
                    <br>
                    Ajay Ajit Hamlai, director of the Ajay group, told OER that the RO8.0 million Markaz Al Bahaj (which loosely translates as 'centre of excitement') will be a three-level complex with around 700 parking spaces and a total shopping area of about 32,500 square metres, about one quarter less than the sales space in the City Centre Muscat. In addition, there will be a basement with a parking area and two-screen cinema.&nbsp;<br>
                    <br>
                    The name of the retailer taking the anchor store in Markaz Al Bahaj has not yet been announced but senior management told OER that plenty of interest has been expressed so far in this and the other 80 units on offer. "We don't see ourselves as competing with City Centre Muscat but as complementing it. Indeed, our mall will not have a supermarket anchor store but more of a general department store," says Keith Holt, group general manager of the Ajay group of companies. "It would be utter madness to go into direct competition with Carrefour."<br>
                    <br>
                    "However, I don't think Carrefour realise they have serious competition in the Sultanate compared to Dubai. Here in Oman, there's Sultan Shopping, Al Jadeed Shopping, the Sultan Centre, Pic'n'Save and Lulu Shopping which has dominated retailing in Salalah. You can expect retailers to be marketing aggressively as competition in the next few months will be fierce. The shopper is going to be in a wonderful situation," he added.<br>
                    <br>
                    Like many before it, the newly-opened LuLu Shopping Centre in Ruwi has recently launched an aggressive marketing promotion with six new cars up for grabs every fortnight until April. Carrefour officials say that they will be ready with unrivalled special offers and deals as well as regular promotions as soon as it opens.<br>
                    <br>
                    The Carrefour hypermarket will offer over 60,000 products from a shopping area of over 8,000 square metres. Carrefour will employ over 400 staff which will include a large number of Omanis, says one project manager. There will be over 40 cash counters with dedicated counters for express service to customers with few items. The opening hours of the shopping centre will be from 9am to midnight except Fridays when it will open at 1pm.<br>
                    <br>
                    Oman is set for a massive boom in retail development, new openings and refurbishments despite complaints from local retailers about disappointing consumer spending in 2000. "Its not that people had less to spend last year. Shoppers just seem to find Dubai more attractive as a retail destination and the perception of Dubai as cheap for all types of goods including electrical items is a problem for us," says the general manager of a well-known shopping complex in Qurum.&nbsp;<br>
                    <br>
                    A new Sultan Shopping Centre is ready to open near Seeb, a new Al Burj is being built 3km north of the Muscat City Centre while Mutrah Cold Stores (MCS) LLC, a partner of the W.J. Towell group of companies, announced on February 16 that it will be assuming control of the Al Fair supermarket chain in the Sultanate. MCS will acquire the stores from Technical and Consumer Services Co. LLC. MCS will acquire six supermarkets at Al Khoud, Nizwa, Zakher Mall (Muscat), Shatti Al Qurum, Ruwi and Wadi Kabir to add to its existing chain of stores at Madinat Sultan Qaboos, Al Sarooj and Salalah.<br>
                    <br>
                    "For Muscat Cold Stores, more retail outlets means more opportunities to improve the profit margin," said Keith Holt of the Ajay Group.<br>
                    <br>
                    On the current retail climate in the Sultanate at present, Vicki Senior said: "Oman is quite cheap to trade in. It doesn't have big rents like Dubai but you've got to run with very tight staffing costs to keep costs down and margins at a reasonable level."</font></p>
