</font><b><font face="Verdana" size="2">Life in positive mode</font></b><font face="Verdana" size="1"><br>
<b>Nilesh Samani loves everything around him, including his family, friends and 
his work, writes Visvas Paul D Karra</b><br>
<br>
<img border="0" src="beyond.jpg" align="right" hspace="6" vspace="4" width="250" height="220">I love to spend time with my soon-to-be five-year-old daughter and take her on 
outings,” says Nilesh Samani, general manager of Oman United Agencies (OUA), as 
part of his opening remarks when we caught up with him at his office in Madinat 
Qaboos. Samani’s office, brightly lit with sunlight pouring in from the large 
windows, reflects the cheerful personality who spoke to us about his many 
passions in life.<br>
<br>
<b>A love for sports</b><br>
For instance, although Samani was born and brought up in Mumbai, the commercial 
capital of India, he loves to tuck into South Indian food, especially idli and 
dosa.<br>
<br>
<img border="0" src="beyond1.jpg" align="left" hspace="6" vspace="4" width="192" height="170">When asked about the cream-coloured cricket hat perched on one of his side desks 
with his first name written on it, Samani informs that he loves sports. 
Naturally, as an Indian, he loves cricket and is an avid cricketer and a good 
all rounder at that. An active sportsman during his stay at Jai Hind College in 
Mumbai, he went on to win a gold in chess, and was an ace at carrom and table 
tennis. This affable gentleman also loves to hang out with his friends and their 
families on the weekends as OUA works on a five-day week. It is more than just a 
friendly gathering, as they usually end up cooking and eating some of the 
popular Mumbai road-side snacks like ‘vada-pav’, ‘misal pav’ etc. When at home, 
Samani likes to laze in front of the television watching news, mostly business 
channels, as he dabbles big time in the share market. <br>
<br>
<b>Workaholic to the core </b><br>
At work though there is no compromise and he spends nearly 16 hours at work on 
most days. As the general manager of OUA, his business entails frequent 
travelling and staying out late hours while in Muscat. So while his colleagues 
grumble that he is a workaholic, his wife has figured it out that she is third 
or fourth on the list of his priorities. He admits with a gentle smile and a 
twinkle in his eye, “I try to balance hard between work and family as I fully 
believe in our company’s credo ‘bringing more to life’.”<br>
<br>
A firm believer in investing in people, he is quick to point out the virtues of 
his colleagues and his employees. He unabashedly confesses that he was the only 
one who came in a business suit while all his other Indian colleagues came to 
office dressed in their traditional attire on the occasion of India’s 
Independence Day. <br>
<br>
A positive attitude helps him take on challenges in his stride. “There is 
nothing called ‘no’ in my dictionary. With the right attitude and the right 
approach you can achieve everything, For example, there are many people who say 
that Oman lacks enough events but I hardly find any free time because I have so 
many activities to do both at work and leisure,” points out Samani. It is no 
wonder then that this go-getter, who came to OUA as a chartered accountant seven 
years ago, is now the general manager of the company. </font></p>

