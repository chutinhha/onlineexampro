<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">FIRED UP</font></b><font face="Verdana" size="1"><br>
<b>Enterprises, like human beings, have their own metabolic phases: Growth, 
sickness, recovery and decay. Majan Glass – the one and only glass manufacturer 
in the Sultanate of Oman – was no exception. Ramesh Kumar and Fatma al Arimi 
report</b><br>
<br>
<img border="0" src="corp.jpg" align="left" hspace="6" vspace="4" width="250" height="291">Promoted in 1994 and established a year later following the market study by Tata 
Economic Consultancy Services, Majan Glass began with a bang in 1997, but ran 
into trouble owing to emerging ground realities beyond its control. Dwindling 
supply orders coupled with promoter inertia to pump in additional capital to 
tide over financial bumps and the consequent high cost borrowing dealt a severe 
blow to the very survival of the prestigious company.<br>
<br>
The Ministry of Commerce and Industry immediately took charge of the situation 
and in concert with banks went in for a major restructuring. Today, Majan Glass 
is a healthy corporate entity, having wiped out all losses and holding its head 
high. In fact, the government has announced its plan to go in for dilution of 
its equity soon.<br>
<br>
Majan Glass has transformed dramatically under the dynamic chairmanship of Anwar 
Ali Sultan, who has been in the saddle right from inception. The bottomline has 
improved so much that it also prepaid its loans to a leading bank in totality as 
a lump sum settlement – at least 8 years ahead of schedule.<br>
<br>
Hang on. Majan Glass also won “His Majesty’s Certificate for the Highest Value 
Addition for the Year 2005” and His Majesty’s Shield for the 5 Best Performing 
Companies for the Year 2007”.<br>
<br>
Incredible, no doubt. Sultan shares his experience in turning around the sick 
company in an exclusive interview with OER. Excerpts:<br>
<br>
<b>
<img border="0" src="corp1.jpg" align="right" hspace="6" vspace="4" width="300" height="249">What was it like when some of the promoters refused to bring in fresh capital 
to survive in the marketplace?</b><br>
It was tough. A lot of study and hard work has gone in setting up Majan Glass. 
To kill an industry is a sin because you have sunken so much of money.<br>
<br>
<b>So, what did you do?</b><br>
We approached the government to come to our rescue. We sought financial help. It 
took a lot of time for it to respond because the government was busy chalking 
out revival plans for several other government owned companies.<br>
<br>
<b>What led to the crisis in the 1990s?<br>
</b>As far as Majan Glass is concerned, there were two factors that created 
challenges. In 1996, the government decided to decrease the volume of loans as a 
result of which Majan was forced to go for a commercial loan to cover the 
shortfall between the entitled loan amounts and sanctioned loan amounts at very 
high interest rates. Secondly, our study said that our breakeven target price 
perceived to be achieved on inception in 1996 went down by an extent of roughly 
45 per cent.<br>
<br>
We survived, thanks to strategic sales to a customer from UAE, which kept us 
going. And that is when the government acquired Majan Glass which came as a 
great relief and the original promoters left. The government infused roughly 
RO1.5mn at that time.<br>
<br>
<b>
<img border="0" src="corp2.jpg" align="left" hspace="6" vspace="4" width="189" height="302">Then, what happened?<br>
</b>We commenced with two lines of production (250 metric tonnes per day 
capacity) to produce green and flint (white) glass i.e. one machine per furnace 
initially on commencement.<br>
<br>
Soon after the government takeover the company turned around as a result of 
reorganising its setup with new and experienced management team, developing new 
customer base and product mix, selling its production capacities at high 
realisation prices against long term contracts finalised with new customers and 
renegotiating and developing new and competitive supply contracts with all 
Material Suppliers. The Company started to improve its performance and firmly 
turned the corner making net profit at the end of 2004.<br>
<br>
With this and the potential the Glass requirements of existing and new customer 
base, the management proposed an expansion of capacities by restarting the idle 
second furnace which was closed down due to lack of orders in 1999 and order the 
third line at an estimated cost of RO1.75mn, which was reviewed and approved by 
the Board of Directors. All of a sudden, our future had begun to look rosy. We 
went in for the third line financing purely through internal accruals.<br>
<br>
Once the approval for the third line came through in 2006 and we commenced the 
line in November 2006, The management again came back to the board of directors 
after a review proposing to order the fourth line immediately justifying the 
increasing demand of glass and our need to encash on it.<br>
<br>
Our finances were in excellent shape. Above all, the cost of setting up the 
fourth line would also work out comparatively more economical than we 
anticipated as we were the only company in the Middle East planning one 
expansion after the other in a short span and had definitely a better purchasing 
and bargaining power compared to that during inception. Emhart Glass of 
Switzerland, one of the original technology providers, supported our expansion 
by offering very competitive price for the fourth line , while we also 
negotiated successfully simultaneously to get the inspection machines on our 
first and second production lines upgraded within the new order.<br>
<br>
<b>The government fund infusion was the turning point….</b><br>
Yes. We commenced our operations with two furnaces – one for green bottles and 
the second for flint. Though the life of furnace is seven years, we decided to 
shut down one of them in 1999 due to inadequate orders. In 2006, we got it 
re-commissioned with our order book getting full.<br>
<br>
Now we are in the process of planning the rebuild of our bigger furnace of 150 
tons per day by the year end on completion of the estimated furnace life span 
and special refractories ordered for the same rebuild are already received in 
the plant. This planned rebuild is expected to be completed within approximately 
three months from date of commencement.<br>
<br>
<b>Is it not going to affect your capacity and hence your ability to service the 
market?</b><br>
Not at all. The management has clearly planned the stock building exercise well 
in advance to ensure that the customers are not impacted in any manner during 
the rebuild.<br>
<br>
<b>There is a lot of talk about government divesting its stake in Majan Glass. 
What is the status?</b><br>
At present, government holds 75.3 per cent. The government decision to divest is 
no secret. We don’t know when it will happen. A committee was formed a few 
months ago to take it forward, but it has not met us so far.<br>
<br>
<b>Any idea on the pricing?</b><br>
How can I say? The government has to decide the ideal price. I reckon it will be 
a float at a reasonable price.<br>
<br>
<b>Are you happy with the performance of Majan Glass scrip on Muscat Securities 
Market (MSM)?</b><br>
We have not done any study on this aspect. However, it touched an all time high 
recently. There is no big shuffle in Majan Glass scrips. It is among the top 10 
shares traded on the exchange.<br>
<br>
<b>What else is happening?<br>
</b>The fourth line commenced production since September last year and as I 
said, it has helped us in our stock building.<br>
<br>
<b>Won’t the shutdown for rebuild affect your customers?<br>
</b>No. The Management has been systematically planning stocks since some time 
and building up inventories so that the shutdown does not impact meeting 
customer demands. At any point of time since 2004, we have been operating at 100 
per cent optimum capacity utilisation.<br>
<br>
<b>How is business?</b><br>
We are doing well. We have invested into value-added features like online 
labelling and ACL printing. The exposure of Omanis to the external world is 
creating market for new products in food, juices and assorted sectors which in 
turn is driving demand for our products. We have flexibility having two furnaces 
where we run flint on One furnace and green on the other to shuffle between 
flint and green bottles to meet changing and growing market needs. We constantly 
watch out and calibrate our production needs accordingly. The growth has been 
steady for the past 4-5 years.<br>
We cater to 100 per cent of the domestic demand and balance capacities are 
exported to 17 countries in addition to major supplies within the GCC. We are 
approved suppliers to Pepsi Cola and Coca Cola International system franchisees 
worldwide.<br>
<br>
<b>What’s happening on the technology front?</b><br>
We use cutting edge, state-of-the-art technology. Today our standing is very 
high and all technology suppliers want to do business with Majan. Ramesh Mani 
(General Manager) and me are regular visitors to global glass expos to keep 
ourselves abreast of latest in the world of glass.<br>
<br>
<b>How do you feel, when you look back?</b><br>
At one point of time we were unable to do anything. It was tough time. Today, we 
are able to do anything and everything we want. That too, all on our own. I wish 
to thank our banks – NBO, Bank Muscat, Oman Development Bank, PEIE and all 
others for being patient with us when we were servicing our loan. Also some of 
our local customers who understood the trying times we were going through and 
stayed put with us. Of course, our Management and the team deserves praise for 
their hard and sincere efforts without which this would not have been achieved.<br>
<br>
<b>Any challenges on the raw materials front?</b><br>
As far as limestone and packaging are concerned, they are readily available 
inside the Sultanate. Rest of the materials required by us have to be imported.<br>
<br>
To that extent, the imported cost does not make much difference when compared 
with competition as they are also importing the materials similar to us. We are 
looking at converting the local sand for use and are in touch with the concerned 
authorities requesting them for permission. When it happens, it will hopefully 
help us in bringing down our costs.<br>
<br>
Maintaining low cost, achieving optimum efficiencies and selling at the right 
prices. These are the three key factors that our management remains focused and 
maintains a close watch on. As our volume goes up, automatically consumption of 
raw material also shoots up. When you buy in such huge quantities, you are able 
to extract better pricing from sellers for volume based pricing.<br>
<br>
My personal wish is to encourage Omani companies to become regular suppliers to 
Majan Glass. We also want to recommend them to others outside Oman. They have to 
invest in a big way, taking volume into consideration.<br>
<br>
<b>What about the equation between the board and management?<br>
</b>Everybody has contributed – including the board. The board members came from 
the government, banks and reputed industries. The management is headed by a very 
experienced glass professional supported by experienced staff from the glass 
industry. There is a perfect tuning between the board and management.<br>
<br>
<b>Any long term plans?</b><br>
It is too early to share anything now. Yes, we have some plans. But we have to 
wait for the government’s decision towards divestment before we think in lines 
of moving further. </font></p>

