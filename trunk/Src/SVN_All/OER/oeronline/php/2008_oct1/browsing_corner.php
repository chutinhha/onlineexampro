        </font><b><font face="Verdana" size="4">On China Road</font></b><font face="Verdana" size="1"><br>
        <b>Route 312 from Shanghai to Kazakhstan border is an allegory to the 
        road which China will travel in the coming decades, writes Raja Shekhar 
        Chandola<br>
        </b><br>
        <img border="0" src="browse.jpg" align="left" hspace="6" vspace="4" width="200" height="240">In this engrossing and deeply personal book, well known National Public 
        Radio reporter Rob Gifford, a fluent Mandarin speaker, takes the 
        dramatic journey along Route 312 from its start in the boomtown of 
        Shanghai to its end on the border with Kazakhstan, travelling nearly 
        5,000kms. Gifford reveals the rich mosaic of Chinese life in all its 
        modern contradictions, posing the crucial questions that everyone is 
        asking about China: Will it really be the next global superpower? Is it 
        as solid and as powerful as it looks from the outside?<br>
        <br>
        Gifford does a good job of shedding light on some politically 
        controversial issues such as the Chinese control over Tibet and the AIDS 
        villages of Henan province, throwing light on the seismic changes going 
        on culturally, economically and politically. In all cases, he clearly 
        explains all sides of the story, but he sometimes seems too soft on the 
        Chinese government. While he does often point out their shortcomings, he 
        also immediately makes up or apologises for doing so. The book does give 
        a deep insight into the current political and economic state of China, 
        as well as providing a much needed history lesson. China Road, is 
        Gifford’s swansong to the People’s Republic; his final act before 
        leaving the country after spending many years there as a journalist. The 
        book is a very vivid and lively piece of reportage; equal parts 
        travelogue, cultural insight and political analysis, a palatable and 
        engaging vehicle that brings modern China to life and also reveals 
        Gifford’s great story telling ability and love of Chinese culture. <br>
        <br>
        Gifford writes about the vast divide between booming cities and 
        forgotten rural areas, with millions of Chinese migrating across the 
        country in search of jobs, hoping to share in China’s new wealth. He 
        also discusses the country’s attempts to restrain and integrate its 
        minority populations, most notably Tibetans and Uighurs. One of the weak 
        points in China Road is the intrusion of Gifford’s religious views – he 
        frequently lets moral outrage colour his arguments e.g. he argues that 
        modern Chinese are floating in a moral vacuum because Mao eradicated 
        religion. We get an insight into his love-hate relationship with China. 
        He sometimes projects his western moral view in some of his opinions. It 
        is good because he is honest. It is bad because he silently condones the 
        western superiority complex.<br>
        <br>
        Gifford’s experience as a radio journalist makes his descriptions vivid 
        and engrossing. He peppers his writing with pinyin Mandarin and phonetic 
        explanations of how to pronounce Chinese names and the fact that he does 
        not come to a conclusion about whether the nation will sink or swim 
        doesn’t matter in the end. Gifford follows the journalist’s motto of 
        just telling what he sees and explaining the situation. If there is 
        anything that he praises it’s the development of Western China and how 
        though life is still hard for the Laobaixing — the common people — in 
        the region, things have gotten a whole lot better than they used to be 
        and that’s a good thing.<br>
        <br>
        The down side of this approach is that the author could not write about 
        one issue for too long, because the journey has to move on. The book is 
        highly recommended if you’re going to visit China anytime soon, or are 
        just interested in learning more about it. <br>
