				<b><font face="Verdana" size="4" color="#C00000">Fair play</font></b><p align="justify" class="style13">
						<p align="justify" class="style13">
				<br>
				<b><font size="2">As female employees became one of the most 
				significant icons of change, protecting their rights has also 
				assumed importance<br>
				<br>
				<font color="#808080"><i>By James Harbridge</i></font><br>
				<br>
				</font></b>The working culture in Oman has significantly changed 
				since the Renaissance in 1970. As Oman began to liberate its 
				economic system and become a part of the international trade 
				community, female employees became one of the most significant 
				icons of change.<br>
				<br>
				This article will briefly outline some of the Labour Law 
				articles which relate to female employment.<br>
				<br>
				<font color="#990000" size="2"><b>Working hours</b></font><br>
				<br>
				Article 81 of the Labour Law stipulates that an employer shall 
				not require women to work between 7 pm and 6 am, except when 
				otherwise authorised by a Ministerial Decision relating to 
				female working hours. It can be anticipated that this provision 
				will change sooner rather than later.<br>
				<br>
				<font color="#990000" size="2"><b>Maternity leave</b></font><br>
				<br>
				According to Article 83 of the Labour Law, a woman who has 
				completed one year of service with her employer shall be 
				entitled to this leave. The leave is limited to a maximum period 
				of six weeks, which includes both the pre-delivery period and 
				the post delivery period. It is crucial for the woman applying 
				for maternity leave to present a medical certificate stating the 
				expected delivery date.<br>
				<br>
				With regards to pay during the maternity leave period, a female 
				employee is given the right to choose between two options. 
				First, she can opt for the maternity leave to be treated as a 
				leave without pay or, alternatively, she can elect for the 
				maternity leave to be deemed as sick leave, subject to Article 
				66 of the Labour Law which sets out the entitlement to 10 weeks’ 
				sick leave during any given year.<br>
				<br>
				<font color="#990000" size="2"><b>Nature of work</b></font><br>
				<br>
				Article 82 of the Labour Law stipulates that female employees 
				shall not be required to carry out any type of work that is 
				considered to be harsh work or harmful to health or any other 
				works so determined by a ministerial decision.<br>
				<br>
				In conclusion, the Omani Labour law has noticeably outlined some 
				of the major issues concerning the employment of women.<br>
				<br>
				The recent employment figures indicate that the number of female 
				employees in Oman has increased dramatically, suggesting that 
				the laws regulating the employment of women will be amended to 
				cover more substantial issues concerning the female workforce. 
				Indeed, a quick look at employment legislation for women in 
				other countries shows that the topics covered include 
				anti-discrimination laws, absence from work due to fertility 
				treatments, sexual harassment in the workplace and the issue of 
				overtime for pregnant women and nursing mothers. In time, these 
				types of scenarios may well be covered by Omani labour statutes.<br>
