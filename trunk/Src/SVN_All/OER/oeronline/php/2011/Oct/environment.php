				<font face="Verdana" size="1">
				<img border="0" src="images/envirnoment-oct2011.jpg" style="border: 1px solid #808080" class="style4" width="200" height="207" align="right"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>The green initiatives of National Pharmaceutical Industries 
				Co have been a motivating factor for other companies in Rusayl 
				Industrial Estate in terms of cleanliness and greenery, a fact 
				appreciated by visitors to the company</b></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				</font>
				<font face="Verdana" size="1" class="style11" style="font-size: 8pt">
				The National Pharmaceutical Industries Co (NPI) has demonstrated 
				its concern for preserving water by utilising the treated waste 
				water to maintain greenery in its premises consistently over the 
				past 10 years. <br>
				<br>
				Since water is a scare resource, this effort by NPI has thereby 
				been serving a dual purpose – maintaining greenery around its 
				premises while at the same time conserving water. Apart from the 
				saving and preserving water, NPI has an ongoing staff training 
				programme on personal hygiene, cleanliness, cross contamination, 
				good manufacturing practices, all of which lean&nbsp; towards 
				making their environment safe and clean.<br>
				<br>
				The main goals of this concern for environment are to provide 
				the most environment-friendly and healthy work place and at the 
				same time, ensure purity of air and water both incoming as well 
				as outgoing.<br>
				<br>
				</font><b><font face="Verdana" color="#000080" size="2">Green 
				habitat</font></b><font face="Verdana" size="1" class="style11" style="font-size: 8pt"><br>
				<br>
				NPI has a land area of 30,042 sq mtrs. Except for the built-up 
				area, all the other open land has been covered with lawns and 
				trees. The quality of air is also monitored to constantly ensure 
				that the factory complies with class 100,000 specifications. 
				NPI’s main customers are invited to plant a tree in the factory 
				lawns to mark their visit to the plant. This serves both as a 
				token of remembrance of the customer as well as improves the 
				greenery of the environment. As a commitment to green habitat, 
				the maintenance of the lush green lawns is a priority all 
				throughout the year.<br>
				<br>
				The size of green land is 20 per cent as compared to the total 
				site area and it is 40 per cent when compared to open land with 
				five palm and other flowery trees. The waste water generated by 
				the plant is purified constantly through a regular filtration 
				process as well as through a double reverse osmosis plant. The 
				well controlled effluent treatment plant (ETP) ensures the 
				effective management of waste water disposal.<br>
				<br>
				</font><b><font face="Verdana" color="#000080" size="2">HSE 
				policy</font></b><font face="Verdana" size="1" class="style11" style="font-size: 8pt"><br>
				<br>
				Health and safety of the staff is a key objective for NPI. All 
				the air is filtered through high efficiency particulate air 
				filter with controlled humidity and temperature. Drinking water 
				is purified and distributed through an in house supply system 
				avoiding the accumulation of plastic containers. Water for all 
				industrial use is purified in house as a first step, then the 
				water is treated and analysed as per Oman Environmental 
				Regulations and then used for irrigation of the green parts of 
				the site. The air expelled into the atmosphere from all the 35 
				air handling units is very clean as it is filtered through very 
				fine filters that capture particles of more than five microns.<br>
				<br>
				Further, all sanitary waste water outlets are connected to the 
				underground Al Rusayl Industrial Estate System for treatment 
				outside the plant. NPI has a Central Industrial Vacuum System 
				for cleaning the whole plant. The dust collected in this system 
				is not released into the atmosphere or discarded. Instead it is 
				passed through a scrubber and treated and dissolved in water.<br>
