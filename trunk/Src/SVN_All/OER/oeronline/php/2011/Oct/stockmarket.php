						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Promising steady returns</font></b><p align="justify" class="style13">
				<b>
				<span style="font-size: 9pt">SMN Power is confident that its 
				stable cash flows and attractive dividend forecasts will attract 
				investors to its IPO<br>
&nbsp;</span></b><p align="center" class="style13">
				<b>
				<font face="Verdana" size="7">
				<img border="0" src="images/stockmarket1-oct2011.jpg" style="border:2px solid #C0C0C0; " class="style19" width="450" height="504"></font></b><p align="justify" class="style13">
				SMN Power, owns Rusayl Power Company (RPC) and SMN Barka and is 
				the largest power company in the Sultanate with a total 
				generation capacity of 1,343 MW – representing around 35 per 
				cent of Oman’s Main Interconnected System (MIS) capacity. The 
				company is diluting 35 per cent stake in line with a contractual 
				commitment with the government. Johan Van <font color="#000080">
				<b>Kerrebroeck</b></font>, CEO and Frederic
				<font color="#000080"><b>Halkin</b></font>, CFO, SMN speak to 
				Mayank Singh about the IPO’s prospects. Excerpts:<br>
				<br>
				<b><font size="1">What kind of a reaction are you expecting from 
				retail and institutional investors to the IPO?</font></b><br>
				<br>
				<font color="#000080"><b>Kerrebroeck: </b></font>We are looking 
				at a good response from both category 1 (retail) and category 2 
				(institutional) investors.<br>
				<br>
				<font color="#000080"><b>Halkin:</b></font> The proportion is 
				important – 60 per cent of the offering is reserved for retail 
				investors and 40 per cent for institutional investors.<br>
				<br>
				<b><font size="1">If the retail component does not get 
				subscribed do you have a choice of shifting the unsubscribed 
				shares to institutional investors?</font></b><br>
				<br>
				<font color="#000080"><b>Halkin:</b></font> Yes, if this happens 
				then the oversubscription by the institutional investors will 
				compensate for the lack of subscription by retail investors. But 
				we are hopeful that the retail investor will participate in this 
				opportunity.<br>
				<b><font size="2"><br>
				</font><font size="1">Why should anyone invest in the IPO, what 
				is your sales pitch?</font></b><br>
				<br>
				<font color="#000080"><b>Kerrebroeck: </b></font>We have two 
				power generating assets and one is producing power and water as 
				well. We sell our product to Oman Power and Water Procurement 
				Company (OPWP), which is in turn owned by the Ministry of 
				Finance and these are contracts that stretch over very long 
				periods of time, roughly over 15 years. Everyone knows that our 
				reliability and respect for contracts is very high. Once you get 
				a contract of this nature you can be sure about your revenue and 
				cash flows on the input side of the company for many years. On 
				the expenditure side, fuel is one of the major expenditures for 
				a power generating company and the lions share goes into natural 
				gas. Natural gas is again sourced from the Ministry of Oil and 
				Gas, which is again a government owned body. The other terms of 
				the contract too are aligned with each other. This means that 
				when one expires the other expires as well, if one gets extended 
				then the other gets extended as well. Thus the company gets its 
				energy from the government and sells the water and power 
				produced to the government. Therefore while there is an 
				operational aspect, it is important to understand that such a 
				business model leads to an extremely predictable and stable 
				business and cash flows. There are a number of investors who 
				have a appetite for such certain, reliable and forecastable 
				investments and they can buy shares in a company like ours. From 
				that perspective it is a welcome IPO in the market.<br>
				<br>
				<b><font size="1">While it is a stable stock to own, power 
				stocks are usually seen as being defensive stocks without much 
				of an upside. Given this background why should retail investors 
				invest in the IPO?</font></b><br>
				<br>
				<font color="#000080"><b>Kerrebroeck:</b></font> Investors 
				should be excited because the returns are extremely predictable 
				and stable. Then you can look at whether the results are 
				attractive or not, you could offer something with a low return 
				or high return, if you look at the prospectus of the IPO, the 
				returns via dividends and price setting given by BankMuscat our 
				project manager is very favourable from the perspective of an 
				investor.<br>
				<br>
				<b><font size="1">There have been some concerns about your 
				pricing with analysts opining that the price of RO3.52 per share 
				is on the higher side. What is the justification for such a high 
				price?</font></b><br>
				<br>
				<font color="#000080"><b>Kerrebroeck:</b></font> There are 
				different methods of arriving at a price, one is to look at the 
				future cash flows and based on it you calculate the value of the 
				stock, another approach is to look at various ratios like PE 
				multiples, book values etc. And finally when we compare what we 
				are offering to what has been offered in the past then we can 
				say with all honesty and clarity that a price of RO3.52 is not a 
				high price to be paid.<br>
				<br>
				<font color="#000080"><b>Halkin:</b></font> The yield on the 
				share is 12 per cent in 2012 with a 440bzs dividend per share 
				and from 2013 it will be 380bzs per share, which translates to a 
				yield of 11 per cent per year.<br>
				<br>
				<b><font size="1">The dividends that you are promising for the 
				coming years is more than the EPS. Does this mean that you will 
				be digging into your reserves to pay dividends. Secondly, are 
				such high dividends being promised to justify your pricing?</font></b><br>
				<br>
				<font color="#000080"><b>Halkin:</b></font> The company already 
				has had a track record and it has accumulated some retained 
				earnings over the years. We were not able to distribute these to 
				the existing shareholders, due to the technical specifications 
				of the loan agreement, meaning that a series of conditions had 
				to be reached for the company to get the permission of the 
				lenders for the company to distribute dividends. Those 
				conditions were reached by early August 2011 and so now we are 
				in a position to deliver good dividends to our shareholders.<br>
				<br>
				<b><font size="1">You get paid by the government on your ability 
				to maintain a certain generating capacity at any given time. 
				Given that RPC is an old plant there are apprehensions about 
				your ability to sustain generating capacity and a breakdown may 
				impact your cash flows?</font></b><br>
				<br>
				<font color="#000080"><b>Kerrebroeck:</b></font> You may say 
				that RPC is an old plant, but I would say that it is not a new 
				plant rather than an old plant. When we look at the generating 
				portfolio in GDF Suez there are many plants that are operating 
				very well with a lifespan which is far more than that of RPC. On 
				top of this RPC has been extended three times, so while the 
				first generators and alternators date back to 1984, the most 
				recent ones were installed in 2000. Secondly, we have the 
				support of important shareholders like GDF Suez who have a long 
				experience in operating plants. We are confident that we can 
				maintain RPC upto the requisite standards by having a dedicated 
				operations and maintenance programme. These plants can be 
				operated for very long periods of time. In Belgium there are 
				plants which date back to 1966 and those plants operate in 
				environments that are much harsher than Oman. <br>
				<br>
				<b><font size="1">The company is footing a certain part of the 
				expenses of the IPO. Is this true and what is the rationale 
				behind it?</font></b><br>
				<br>
				<font color="#000080"><b>Halkin:</b></font> The cost of the IPO 
				and transfer of assets is being borne by the holding company, 
				this was a decision taken by the board and the impact of this on 
				the valuation of the company has been factorised. The investors 
				subscribing to the IPO should take into account that the holding 
				company is waving the cost. There are two reasons for doing 
				this, when we speak about IPO costs we also have to talk about 
				the cost being undertaken for the transfer of assets from the 
				existing shareholders to Omani investors. Secondly, practically 
				it is better to centralise all costs at one place so that they 
				are shared proportionally by the shareholders according to their 
				participation in the company.<br>
				<br>
				<b><font size="1">The Gulf markets are not going through the 
				best of times, are you worried about the timing of the IPO?</font><font size="2"><br>
				</font></b><br>
				<font color="#000080"><b>Kerrebroeck:</b></font> When the 
				shareholders set up the company there were multiple contracts 
				and in one of the agreements called the Project Founding 
				agreement, there is a requirement that mandates the company to 
				list 35 per cent of its shares before October 31, 2011. We have 
				seen a lot of interest in the IPO and are not worried about the 
				timing. <br>
