						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Simple but not simplistic</font></b><p align="justify" class="style3">
						<span class="style5"><strong>This book is a readable 
						introduction to many aspects of economics and how they 
						play out in everyday life. <br>
						<br>
						<font color="#808080"><i>An OER review</i></font><br>
						<br></strong></span><br><font face="Verdana" size="1">
						<img border="0" src="images/browsing1-oct2011.jpg" style="border-style:solid; border-color:#808080; float: right" class="style4" width="200" height="302"></font>The 
						promise of The Undercover Economist is that it shows you 
						the world through the eyes of the economist. On the face 
						of it that doesn’t sound like a thrilling prospect. 
						After all, economics is about abstract and arcane 
						matters like exchange rate policies, interest rates and 
						monetary policy. The surprise of course is that far from 
						being a dull affair, the book offers a pretty 
						interesting experience.<br>
						<br>
						<font color="#000080" size="2"><b>Day to day affairs</b></font><br>
						<br>
						The book opens by wondering about the price of a cup of 
						coffee at Waterloo Station, in London. Why does a cup of 
						coffee cost what it costs there? What factors govern the 
						price? What influence does the fact that it’s a busy 
						station have on the price? These everyday considerations 
						lead directly to thinking about the basic economic 
						principles at play all around us. And, with concrete 
						situations as examples Harford uses everyday language to 
						explain and illuminate the surprising ways in which 
						economic interactions manifest themselves.<br>
						It’s not just coffee bars that get the ‘undercover’ 
						treatment. Ever wonder why supermarket ‘value’ brands 
						look so awful? There’s an explanation here for that too, 
						and it too relates to coffee bars and the proliferation 
						of different types and sizes of coffee on offer to the 
						confused customer. The examples that the author gives 
						are engaging and interesting. <br>
						<br>
						<font color="#000080" size="2"><b>Higher ground</b></font><br>
						<br>
						More weighty matters are not ignored. Having grounded 
						the reader in the rudiments of economic thinking early 
						on, Harford tackles some of the bigger issues. 
						Globalisation, development economics, poverty, the rise 
						of China as an economic super-power are all tackled. And 
						it’s not just international issues, he also looks at 
						domestic British issues such as the National Health 
						Service.<br>
						<br>
						An interesting chapter Why Poor Countries Are Poor 
						evaluates the role corruption and kleptocracy play in 
						keeping countries poor. Harford points out that 
						corruption is not only unfair, but it’s also ineffective 
						at distributing resources and growing a country’s 
						economy. As one resident of a poor country notes, even 
						if the country receives massive financial aid to improve 
						infrastructure, it doesn’t matter, because the dictators 
						tend to pocket the money. And, what is actually spent on 
						infrastructure is often wasted.<br>
						<br>
						People have little incentive to get an education in a 
						corrupt country, because people don’t receive jobs based 
						upon meritocracy. Jobs are given to political cronies. 
						Governmental institutions then become dysfunctional. The 
						institutions tend to become more corrupt. The lesson: 
						institutions do matter, and an objective rule of law is 
						crucial to a country’s economic well-being. The citizens 
						of a country ultimately pay for political corruption.<br>
						<br>
						While the book is clearly aimed at the general reader, 
						there are plenty of pointers and references for those 
						who are intrigued and want to learn more. Like 
						Freakonomics, the book is intellectually engaging and 
						goes a long way to show that economics is not some dry 
						academic discipline but a vital tool for understanding 
						the world as it is. This book is, needless to say, 
						highly recommended.<br>
