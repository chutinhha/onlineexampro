						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Going beyond</strong></span><p class="style15">
				<strong><span class="style14">World renowned economist Prof 
				Jeffrey D Sachs, Director of The Earth Institute at Columbia 
				University and Special Adviser to United Nations 
				Secretary-General Ban Ki-Moon, was recently in Muscat to deliver 
				a talk on ‘Challenges Facing the World Economy in the Coming 
				Decade, Sustainable Development and Eradication of Poverty’ 
				organised by <font color="#808080"><i>BankMuscat. Muhammed Nafie</i></font> 
				catches up with him for a quick interview</span></strong><p class="style15">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview1-sep2011.jpg" width="450" height="270" style="border: 1px solid #808080; "></font><p align="justify" class="style13">
				<b>What will be the major challenges of the impending 
				geopolitical change in the aftermath of financial crisis?</b><br>
				<br>
				We are certainly moving to a more multipolar world where the US 
				dominance has dramatically diminished and the rise of China, 
				India, Brazil and other powers is great significant. The fast 
				growing countries in the world are now the emerging markets of 
				the developing world; both Europe and the US have grinding 
				economic problems. The decline of Europe’s share of population 
				shows the decline of its geopolitical weight, just like the 
				decline of the US’s geopolitical weight due to the rise of 
				emerging economies. So this is reshuffling the global balance, 
				not making it easier, however to solve global problems like 
				climate change because global cooperation in this area is very 
				difficult. The US is looking inward at its own domestic 
				political debates, Europe is looking to save the Euro and the 
				Euro zone, and China and India do not have the tradition to lead 
				globally, and therefore we have a period where global leadership 
				is very difficult and global cooperation is very slow in 
				addressing major challenges.<br>
				<br>
				<b>How serious are world leaders and policy makers about 
				sustainable development? Do you think they are doing something 
				beyond lip service?</b><br>
				<br>
				I defined sustainable development as triple bottom-line economy; 
				ie economic development, fairness in the sense of reaching 
				everybody including the poorest of the poor, and environmental 
				sustainability. And when you think about the triple bottom-line 
				of economic social and environmental goals, we can see that the 
				world is quite far from what we need to accomplish. On one hand, 
				there are still very poor countries trapped in poverty, second, 
				there is a lot of income which has been rising in many places. 
				Third, the environment is definitely worsening at the global 
				level with more shocks, more climate change, more loss of 
				biodiversity and more water diversity. We have not been 
				effective since the Rio summit in 1992 when major treaties were 
				agreed upon. We have not effectively implemented those treaties. 
				And when the world returns to Brazil in the next year for Rio 
				plus 20 summits it will take note of how we have squandered 20 
				years of time instead of making progress.<br>
				<br>
				<b>How effective have the recent measures been in tackling the 
				sovereign debt crisis in Europe?</b><br>
				<br>
				Europe did make some good steps in its negotiations with Greece. 
				But the process is still very difficult and complicated one and 
				by no means is Europe out of danger. So what happened was a step 
				forward but Europe’s process is rather convoluted and 
				complicated. So there is not a completely clear path for working 
				with the Greek government to try to make sure that the best is 
				made out of this agreement so that Greece has enough time to get 
				out of this crisis. I think there is some hope that this is 
				going to happen but the negotiations and the whole process 
				remains complicated.<br>
				<br>
				<b>What do you think about the role of American policymakers in 
				solving the recent problems?</b><br>
				<br>
				Our political process is in a bad shape. We have some very big 
				deep chronic problems not addressed by either political party. 
				It is causing the US to experience kind of an economic 
				stagnation. I have a book coming out in October this year that 
				proposes some new approaches for the US leadership in this 
				regard. <br>
