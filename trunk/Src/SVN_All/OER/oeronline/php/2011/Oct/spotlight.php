				<b><font size="2" color="#000080">Telecom sector</font></b><br>
				<br>
				Mobile penetration rates were 171 per cent at Q4 2010 with 
				4,606,133 subscribers by the end of December 2010. This 
				represents an 8 per cent growth rate from Q3 2010 which also saw 
				the Average Revenue Per User (APRU) increase to R0.17.810 per 
				month for Fixed Post Paid users, an increase of 8.6% against Q3 
				2010. Whilst these levels head towards the saturation points 
				experienced by other GCC countries, such levels of growth remain 
				good news for operators and their shareholders.<br>
				<br>
				Slow initial investment into fixed line technology has been 
				overshadowed by improved mobile technology, connectivity, 3G 
				technology and smartphones. Existing restrictions to VOIP 
				services such as Skype are anticipated to be relaxed within the 
				region. As such, the report indicates that operators will be 
				required to revise their pricing approach and be creative with 
				future data services that they offer.<br>
				<br>
				Combined with Government support and initiatives, which hope to 
				increase the ICT development and knowledge of end users, this 
				will be a key driver in continued growth along with guidance 
				from the Telecommunications Regulatory Authority (TRA). As a 
				result, consideration is required to real estate strategy and 
				portfolio flexibility in order to drive future market position. 
				Operators are likely to adopt a more organic approach to their 
				wider real estate needs, supported by professional property 
				advisors. This will include appropriate estate management 
				services during network deployment such as site acquisition and 
				commercial tenancy agreement management. Furthermore, corporate 
				strategy is likely to see additional real estate requirements in 
				the form of retail outlets to improve customer 
				experience/service and office expansion/consolidation as a 
				result of previous growth or changes in market position.<br>
				<br>
				<b><font size="2" color="#000080">Hotel rooms to increase</font></b><br>
				<br>
				STR Global figures report the average occupancy rate for Muscat 
				hotels in 2010 as being 55.8 per cent. While a slight 
				improvement on 2009 performance, this figure is significantly 
				below the 67 per cent achieved in 2008 and 69 per cent in 2007. 
				Accordingly it is evident that the hotel sector in Muscat has 
				suffered from the global recession.<br>
				<br>
				The Ministry of Tourism figures for the Muscat capital area for 
				2010 show that supply stood at 95 hotels and serviced 
				apartments. The supply of hotel rooms in the capital is 
				currently dominated by the four and five star sector which 
				provides 51.7 per cent of hotel rooms and 48.6 per cent of hotel 
				beds. Despite some fluctuations, development in the number of 
				hotels and hotel rooms in the Sultanate has progressed at an 
				average rate of around 7 per cent per annum over the last five 
				years according to Ministry of Tourism figures. In comparison, 
				the average year on year growth rate between 2000 and 2005 was 
				12 per cent for the number of hotels and 11 per cent for the 
				number of hotel rooms. It is evident that the comparative rate 
				of hotel development in Oman has slowed from 2006 to 2010 in 
				comparison to 2000 to 2005.<br>
				Two areas in Muscat have, however, witnessed marked development. 
				Over the last two years alone, the Al Khuwair area has seen the 
				opening of the 4 star Platinum, City Seasons and Park Inn hotels 
				and the 2/3 star Ibis, Rotana, Samara and Tulip Inn hotels which 
				have added approximately 1,000 rooms to the area’s stock. In 
				addition, there are a large number of unbranded, owner-operated, 
				moderate to low standard hotel apartments being developed in the 
				central to western areas of Al Khuwair, Bausher, Ghubrah and 
				Azaiba over the past couple of years aimed primarily at the 
				budget market.<br>
				<br>
				Hotel projects that we have identified as having a moderate to 
				high chance of being built over the next five years would, in 
				our estimation, add 5,000 to 6,000 rooms to the 3 to 5 star 
				sector in the Muscat capital area: this compares to a total of 
				3,777 rooms in the 3 to 5 star sector in Muscat in 2010 and 
				equates to a potential average annual growth rate of 
				approximately 18 per cent to 21 per cent over the coming five 
				years. In comparison, it is evident that the number of hotel 
				guests is closely linked to the number of inbound tourists and 
				that these have both grown at an average annual rate of around 
				7% over the last 5 years.<br>
				<br>
				It would appear that the rate of growth of inbound tourism will 
				need to accelerate significantly from current levels just to 
				maintain average occupancy levels as the projected pipeline of 
				hotels comes into the market.<p align="justify" class="style13">
