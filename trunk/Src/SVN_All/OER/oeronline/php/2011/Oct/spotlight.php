				<b><font size="2" color="#000080">American hardwoods</font></b><br>
				<br>
				Oman has witnessed an increasing demand for American hardwoods 
				amidst stable real estate growth, which marks an important 
				turning point as customers, manufacturers and other industry 
				players become active again. Positioning itself as a growing 
				market for American hardwoods in the Middle East and North 
				African (MENA) region, Oman saw over $93,000 worth of imports 
				entering the country directly from the US during the first half 
				of 2011, according to a statement issued by the American 
				Hardwood Export Council (AHEC), the leading international trade 
				association for the American hardwood industry.<br>
				<br>
				The value of American hardwood lumber shipped to the MENA region 
				from January to June 2011 increased by 1.3 per cent over the 
				same six months of last year and reached a total of $24.35mn. In 
				addition, the first two quarters have seen exports of US 
				hardwood veneers to the MENA region increase by 15 per cent to 
				$10.66mn, while exports of flooring increased by 5 per cent to 
				$603,217 and logs by 4 per cent to $14.25mn. The Omani 
				construction industry, which was valued at $3.7bn in 2010, is 
				expected to key in more growth and development via the influx of 
				major projects in the construction and other segments. The 
				American hardwood industry is confident that this will lead to 
				even greater demand for US hardwoods in the coming months and 
				years.<br>
