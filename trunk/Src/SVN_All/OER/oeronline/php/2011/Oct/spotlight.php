				<b><font face="Verdana" size="4" color="#C00000">Vibrant mode</font></b><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<img border="0" src="images/spotlight1-oct2011.jpg" width="450" height="359" style="border: 2px solid #C0C0C0"><p align="justify" class="style13">
						<p align="justify" class="style13">
				<br>
				<b><font size="2">In spite of the unsettled political and 
				economic climate in some parts of the Middle East and North 
				Africa, the real estate sectors in the region have recovered 
				well from any effects of the global financial crisis. This has 
				had a positive spin-off effect on the Sultanate as well, as can 
				be seen from the spurt in both retail and commercial space 
				activities in the country</font></b><p align="justify" class="style13">
				<br>
				Oman’s real estate and construction sector is set to derive the 
				maximum benefits as the government is ramping up spending on 
				infrastructure and development. The current influx of 
				construction projects, combined with major projects in the 
				information technology (IT) segment, has ably positioned Oman in 
				a strong and steadfast position along with other emerging 
				economies. The Sultanate of Oman has a vibrant sector with 2.1 
				per cent growth and over RO1.2bn worth of construction projects 
				underway or in the planning stages, according to a recent report 
				from Business Monitor International, a leading business research 
				and intelligence firm.<br>
				<br>
				The BMI report also reveals the significant potential of Oman’s 
				infrastructure sector with a number of contracts having been 
				awarded in 2011 and substantial new investment planned. The 
				momentum in infrastructure sector is expected to bring in an 
				overall 5.06 per cent real growth towards the end of the year. 
				The confidence in the construction segment is backed by Oman’s 
				five year spending plan, which covers 2011 to 2015 and predicts 
				over RO30bn worth of spending with a 113 per cent increase. 
				Significantly, of this total figure, RO6bn will go to the oil 
				industry and RO3bn will be spent on property developments.<br>
				<br>
				<b><font size="2" color="#000080">Office space</font></b><br>
				<br>
				Demand for office space in the capital area has remained 
				relatively steady over the last 18 months, although the unrest 
				in the Sultanate at the start of the year has seen a marked 
				decline in demand recently. However, the majority of demand 
				remains for smaller spaces of up to 500 square metres, whilst 
				flexibility in office design is vital to cater for smaller space 
				occupiers.<br>
				<br>
				One of the key points in the report indicates that the vast 
				majority of office stock in Muscat is currently Grade B or C, 
				with a limited availability of Grade A space. Cluttons expects 
				that this will soon change as a number of large scale, Grade A 
				office developments are due for completion in the near future.<br>
				<br>
				Among Grade A office developments that are due to be released 
				onto the market during the year include the Al Rawaq building in 
				Qurum, Beach Plaza in Shatti Al Qurum, Saud Bahwan Plaza in 
				Ghubrah and the Tilal Complex in Bausher. In total, these 
				developments alone will add approximately 158,000 square metres 
				of Grade A office space to the market.<br>
				<br>
				Cluttons Oman, one of the well known realty firm in Oman 
				foresees rental values for office space continuing to soften as 
				new office space enters the market. The premium for Grade A 
				space in comparison to Grade B space in the same area ranges 
				from 12.5 per cent to 33 per cent. It is evident that the supply 
				of Grade A office space in the Muscat capital area will 
				accelerate significantly over the coming year. However, in an 
				increasingly competitive market, and with strong interest in 
				Grade A office space, many tenants appear unwilling to pay a 
				significant premium in comparison to the rental values for Grade 
				B space in the same area.<br>
				<br>
				The report signals the proposition that landlords of Grade A 
				office space will need to adopt pro-active leasing strategies in 
				order to secure tenants, such as initial rent-free periods or 
				stepped rents over the term of the lease. In addition, Cluttons 
				emphasizes that high quality, professional property management 
				will be vital in attracting and retaining tenants.<br>
				<br>
				<b><font size="2" color="#000080">Residential</font></b><br>
				<br>
				According to Savills Oman, the first half of 2011 saw healthy 
				and increased activity in the residential leasing sector. Demand 
				was sustained for all types of property, particularly villa 
				options within the new ITC developments. Rentals remained under 
				pressure to some degree owing to corporates generally tightening 
				their housing budget allowances. The move of many companies away 
				from the traditional “expatriate package” which included housing 
				allowance to an all in gross salary has resulted in tenants 
				becoming increasingly demanding in terms of value for money. 
				Values for most property types appear to have reached their 
				‘true levels’ and further declines from existing rates are not 
				predicted over the next quarter.<br>
				<br>
				Whilst medium level apartment rentals have held firm over the 
				quarter, there is likely to be some downward pressure throughout 
				the rest of the year owing to the completion of a significant 
				amount of new stock. With some larger companies developing their 
				own staff accommodation, release of formerly rented apartments 
				into the market could potentially result in an oversupply and a 
				consequential softening of rents, a report by Savills Oman 
				stated.<br>
				<br>
				The impact of the new Integrated Tourism Complexes, The Wave and 
				Muscat Hills Golf &amp; Country Club particularly has been 
				significant for the rental market. With both these developments 
				offering high quality housing options in managed communities, 
				rents consistent with those prevailing in more central areas 
				continue to be achievable and demand remains high.<br>
				<br>
				As the enhanced facilities of hotels, shopping and recreational 
				amenities are completed in both these developments it is 
				predicted that there will be healthy rental growth and values 
				will outstrip those of the central areas. Older housing in the 
				traditionally favoured areas that has not been upgraded to 
				modern day standards is now failing to rent quickly and vacancy 
				rates are climbing.<br>
