						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Tim Sebastian to host Oman 
						Debate 2011</strong></span><p class="style15">
				<strong><span class="style14"><font size="2">As in the previous 
				two editions, Oman Debate 2011 will feature top class 
				interactions between government representatives and members of 
				Oman’s corporate world</font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event1-oct2011.jpg" width="150" height="315" style="border: 1px solid #C0C0C0" align="left"><p align="justify" class="style13">
						Oman Debate 2011, the third annual national debate 
						organised by Oman Economic Review, will be anchored by 
						renowned television host Tim Sebastian, the founder and 
						chairman of ‘The Doha Debates’ and former BBC Hard Talk 
						presenter.<br>
						<br>
						The annual debate encompassing key themes that form the 
						business environment of today and tomorrow in Oman will 
						be held on November 2, 2011 at Al Bustan Palace Ritz 
						Carlton Hotel.<br>
						<br>
						Bank Sohar is the strategic partner of the event. Al 
						Habib &amp; Co. is the support partner. Times of Oman and Al 
						Shabiba are the official media.<br>
						<br>
						The hot topic of discussion for Oman Debate 2011 is how 
						to cope up with the developments of the Arab Spring, 
						which has had its repercussions on the Sultanate of 
						Oman. The government has reworked on its priorities and 
						policies and has taken measures to address various 
						issues. This, in turn, has raised a number of 
						questions/concerns among both the public and the public 
						sectors.<br>
						<br>
						Key questions that will be raised by Oman Debate 2011 
						will be based on issues like job creation, raising 
						salaries, sops and benefits and Omanisation. The Oman 
						Debate 2011 will also discuss whether the Sultanate is 
						at the crossroads of a watershed moment in Oman’s 
						socio-economic history where everything that was held as 
						sacred needs to be questioned and redefined.<br>
						<br>
						Invitees to Oman Debate can expect a lot of excitement, 
						thanks to Tim Sebastian’s experience in the Middle East. 
						Sebastian is perhaps best known for being the first host 
						of the BBC’s flagship interview programme Hard Talk – 
						for which he was twice named Interviewer of the Year by 
						the Royal Television Society. He has also won the 
						Society’s Television Journalist of the Year award, as 
						well as a British Academy award for contributions to 
						factual television.<br>
						<br>
						After being recognised as BBC’s most prolific 
						interviewer through ‘Hard Talk’, Sebastian became the 
						moderator of the Doha Debates, a forum for free speech 
						in Qatar. He won the BAFTA (British Academy of Film and 
						Television Arts) Richard Dimbleby award in 1982 and 
						Britain’s prestigious Royal Television Society 
						Interviewer of the Year award in 2001 for the second 
						time in a row.<br>
						<br>
						Sebastian began his journalism career at Reuters in 
						1974, moving to the BBC as foreign correspondent in 
						Warsaw in 1979. He became BBC’s Europe correspondent in 
						1982, for Moscow in 1984 and then for Washington from 
						1986 to 1989. Sebastian is moderator of the Doha 
						Debates, which he founded in 2004.<br>
						<br>
						Oman Debate is expected to have audiences totalling over 
						400 movers and shakers from the government and Oman’s 
						corporate world converging to witness the intense 
						indepth discussions on the state of the economy.<br>
						<br>
						Oman Debate 2011, is being brought to you by Oman 
						Economic Review in association with the Capital Market 
						Authority (CMA) and will continue to maintain its status 
						as an unique platform where influential individuals will 
						share stories of best practices, effective business 
						case-studies and, most importantly, attributes of true 
						leadership.<br>
