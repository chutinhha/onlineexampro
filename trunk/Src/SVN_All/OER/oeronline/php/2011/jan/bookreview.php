				<p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="2"><b>SuperFreakonomics takes 
				economics out of the realm of theory and applies it to real 
				world problems throwing up important insights. A review by</b> 
				<i>Mayank Singh</i></font><b><font face="Verdana" size="1"><br>
				</font></b><font face="Verdana" size="1"><br>
				Irreverence, brashness and an ability to apply the science of 
				economics to everyday problems are what made Freakonomics an 
				icon. In SuperFreakonomics Steven D. Levitt and Stephen J. 
				Dubner pick up the threads where they left it in their previous 
				offering. The book tackles a number of complex themes like the 
				perceived threat of global warming, what drives human altruism, 
				the angularity of terrorists and skews in the prostitution 
				business. Levitt and Dubner challenge various assumptions and 
				come up with insights that are backed by empirical and 
				collateral evidence.<br>
				<br>
				For example they question the threat of global warming and 
				advance arguments about how the scourge is controllable by using 
				simple solutions like pumping sulphur dioxide in the atmosphere 
				or by using the reflectivity of oceanic clouds. These cost 
				effective solution have the potential to ward off the threat of 
				global warming by 2050. If these remedies sound so rationale why 
				are they not being implemented by the comity of nations?<br>
&nbsp;</font><p align="justify" style="margin: 10px; ">
				<b><font face="Verdana" size="4" color="#C00000">Old habits die 
				hard<br>
				</font></b><font face="Verdana" size="1"><br>
				The answer to the question is a recurring theme in the book – 
				ingrained habits are difficult to change and even when this 
				change takes place it is painstakingly slow. In the 1840s Ignatz 
				Semmelweis a Hungarian born doctor in Vienna’s General Maternity 
				Hospital, proved that the high death rate of mothers and 
				newborns during childbirth was the result of ‘cadaverous 
				particles’ being transmitted by doctors from dead bodies to the 
				women, giving them puerperal fever. Doctors and students were 
				ordered to disinfect their hands in a chlorinated wash after 
				performing autopsies and this saved the lives of 300 mothers and 
				250 babies in a year’s time. One would assume that this simple 
				solution would find ready acceptance amongst doctors all across. 
				Strangely, Semmelweis intervention was ridiculed by doctors and 
				he ended up in a mental asylum dying a premature death.<br>
				<br>
				Similarly when Robert McNamara fitted Ford cars with seat belts 
				in an effort to reduce fatalities in the 1950s and 1960s he 
				encountered the same problem faced by Semmelweis a century ago – 
				changing human behaviour. The authors argue that the cleverest 
				engineer, economist, politician or parent may come up with a 
				cheap, simple solution to a problem, but if it requires people 
				to change their behaviour it may not work as old habits die 
				hard.<br>
				<br>
				Everyday billions of people around the world engage in acts that 
				they know is bad for them – smoking cigarettes, gambling 
				excessively, riding a motorcycle without a helmet because they 
				derive pleasure, thrill or just a break from their daily routine 
				and getting them to change their behaviour even with the most 
				fiercely rational argument may not be easy. SuperFreakonomics 
				throws up a number of such instances where we may be missing the 
				trees for the woods. And at times it may be imperative for 
				researchers and policy makers to look beyond the obvious.<br>
