				</b><font face="Verdana" size="1"><br>
                The government is working assiduously on diversifying the 
				Sultanate’s economic base as part of the Vision 2020. Free zones 
				are expected to be major contributors to this exalted goal. The 
				first phase development of Freezone Sohar has the potential to 
				transform Al Batinah region into Oman’s industrial and economic 
				powerhouse. The project is being developed by Sohar Freezone 
				LLC, a newly established joint venture of Sohar Industrial Port 
				Company (SIPC) and SKIL Infrastructure Limited of India.<br>
				<br>
				Sohar Freezone has been awarded a concession by the government 
				to develop, operate and manage the 4,500-hectare free zone 
				adjacent to the industrial port. Freezone Sohar is proposed to 
				be executed in five phases. Envisaged in the first phase, 
				covering an area of 500 hectares, are a number of downstream 
				industrial and petrochemical ventures, warehousing and logistics 
				services, and so-called ‘soft’ investments, such as educational, 
				medical and other service-related amenities. Investors are 
				expected to enjoy a host of competitive advantages by virtue of 
				the free zone’s strategic geographic location outside the Strait 
				of Hormuz, and its access to world-class logistics. Equally 
				advantageous is the Zone’s proximity to the major markets of the 
				Gulf, Middle East and the Indian sub-continent.<br>
				<br>
				Similarly the Salalah Freezone offers a mix of industrial, 
				manufacturing, warehousing, logistics, distribution, R&amp;D and 
				office facilities, retail outlets, resort, and residential 
				space. Focused on creating vibrant communities, it is being 
				developed in multiple phases over an area of 2000 hectares. Its 
				phase one provides 200 hectares of distribution, logistics, 
				freight forwarding and manufacturing facilities. This includes 
				state-of-the- art infrastructure like road, power, water, 
				sanitation and telecom networks in addition to other various 
				services.<br>
				<br>
				This issue’s cover story takes an in-depth look at free zones in 
				Oman and their potential contribution to the Sultanate’s 
				economy.<br>
