						<p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/jan_aviation.jpg" width="200" height="147" align="right" style="border: 1px solid #808080" hspace="12"></font><font face="Verdana" size="2"><b>Despite 
				being a new entrant to the aviation industry, flydubai has 
				recorded a steady growth.</b><i> Rana Alawneh</i><b> reports from Beirut</b></font><font face="Verdana" size="1"><br>
				<br>
				<br>
				All forecasts predict a remarkable growth in the number of 
				inbound and outbound passengers at low-cost carriers as they are 
				expected to capture 10 per cent of the local and regional market 
				in 2011. This is mainly attributed to their capability to meet 
				different needs of passengers by offering them affordable rates.<br>
				<br>
				It was in June 2009 that Flydubai, Dubai’s first low-cost 
				airline, had announced daily services to Beirut. In the first 
				three months of its operation, the company managed to transport 
				100,000 passengers. This was followed by the launching of 27 
				more new destinations.<br>
				<br>
				Flydubai owns 12 new 737-800s, the most successful brand of 
				Boeing commercial fleet. As of today, there are 1250 Boeing 373 
				flights, with one flight taking off each 4.6 seconds. The 
				company is looking forward to own 50 more new 737-800 by 2016 to 
				operate its flights to no less than 70 destinations in the 
				region.<br>
				<br>
				Speaking on the occasion of receiving the first Boeing 737-800 
				Sky Interior in Beirut, Flydubai CEO Gaith Al Ghaith says the 
				plane is designed to ensure high level of passenger comfort and 
				reduce emissions. The interior design has subtle mood lighting 
				to simulate nighttime, daytime and sunrise/sunset light, as well 
				as redesigned overhead bins to give more cabin space and 
				increased storage.<br>
				<br>
				Flydubai’s new ‘Fiber To The Screen’ (FTTS) in-flight 
				entertainment (IFE) system from Lumexis is lighter and thinner 
				than previous models, enabling it to fit comfortably into the 
				Recaro passenger seats that flydubai has had fitted in its 
				aircraft. Passengers travelling on these new flydubai aircraft 
				can choose what they want to watch depending on their 
				preferences and how much they want to spend. Many carriers are 
				assessing flydubai experience to measure the efficiency of the 
				system and benefits. <br>
				<br>
				Ghaith Al Ghaith says that the reason behind the investment in 
				the new entertainment system is the fact that entertainment is 
				one of the basic needs for most passengers. “On the other hand, 
				we, as a low-cost carrier, cannot afford the high cost 
				traditional entertainment system. The new Fiber To The Screen’ (FTTS) 
				in-flight entertainment (IFE) system from Lumexis is the ideal 
				solution for us as it is known for its high quality, flexibility 
				and low cost.”<br>
				<br>
				The low-cost travel model has contributed to motivating the air 
				travel market in the region. The statistics released by IATA 
				point out that the share of low-cost flights in the region is 7 
				per cent of the total air travel market in 2010. When this level 
				is compared against the more mature markets such as Europe and 
				U.S.A (35 per cent and expected to be 50 per cent in 2011), the 
				level achieved in the region is still very low. This is due to 
				the low penetration rates and untapped potentials.<br>
				<br>
				Commenting on this Al Ghaith says that the growth rate in Europe 
				persuades us to take more efforts to achieve similar rates. “In 
				our region, we need to get visas to move between one country and 
				the other. This in fact acts as a hindrance to the air travel 
				growth. Moreover, the low-cost carriers are still at early stage 
				but have good potentials for growth in the coming years 
				especially if the business environment is conducive, “he adds.<br>
