				<br>
                Oman LNG has launched new vision for pioneering social 
				investment programmes across the Sultanate and announced its 
				‘Young scientist of the year competition’ in 2011. The 
				announcement was made at a special Oman LNG event to mark the 
				10th anniversary of the company’s social responsibility 
				programme. Dr Mohammed bin Hamad al Rumhy, Minister of Oil and 
				Gas, was the guest of honour at the event which was hosted by Dr 
				Brian Buckley, CEO, Oman LNG.<br>
				<br>
				Dr Rumhy was presented with the first copy of ‘Empowering 
				People, Building the Future’, a book that reflects on the 
				successes of the company’s corporate social responsibility 
				programmes in Oman over the last decade. Demonstrating the 
				company’s commitment to the environment and preservation of 
				natural resources, the book has been published on totally 
				recycled paper using vegetable based ink. Outlining Oman LNG’s 
				new vision for the coming years, Dr Buckley said the company’s 
				social investment programme would focus on the three core areas: 
				empowering communities, championing innovation and investing in 
				people.<br>
				<br>
				The ‘Young scientist of the year’ awards, a key initiative in 
				2011 in association with the Ministry of Education, is intended 
				to encourage the creative application of science in secondary 
				schools to meet the socioeconomic and environmental challenges 
				and opportunities in Oman today. It is aimed at recognising the 
				contributions of individual students and schools to innovation 
				in science at both the regional and national levels.<br>
				Commenting on Oman LNG’s Social Investment Programme over the 
				last ten years, Nasser bin Khamis al Jashmi, the Chairman of 
				Oman LNG and Under-Secretary of the Ministry of Oil and Gas, 
				says, “Oman LNG has made a hugely positive impact on the Omani 
				economy and the development of the nation over the past 10 
				years. Oman LNG has become a role model for good corporate 
				citizenship in Oman and a trusted partner for the development of 
				the neighbouring communities where its operations are based and 
				across the nation in partnership with the government and 
				recognised non-government agencies.<br>
				<br>
				Oman LNG sets aside 1.5 per cent of its annual income after tax 
				to invest in a wide range of project areas, including education, 
				healthcare, social infrastructure, culture and heritage, sport, 
				women’s initiatives, vocational training and the environment. 
				The company has invested over RO35mn in 270 projects over the 
				last ten years.<br>
				<br>
				Some of the high profile projects that Oman LNG has supported in 
				the past 10 years include investing RO1mn in the Ras Al Jinz 
				Turtle Reserve, investments of over RO1.5mn in modern 
				laboratories and workshops in the technical colleges of Ibra, 
				Salalah, Nizwa and Musannah, over RO3mn for the 48 training 
				programmes that have placed over 1,400 Omani citizens in 
				employment and support for local sports clubs in the Sur area.<br>
