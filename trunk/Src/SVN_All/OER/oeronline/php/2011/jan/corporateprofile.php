		        <td valign="top" width="100%"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Untapped 
					potentials<br>
					</font></b><font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><b>With some important 
					expansion plans in the pipeline, Sharq Sohar Steel Rolling 
					Mills is all set to meet the increasing demand for steel 
					reinforcement bars in the Sultanate, says 
                    </b><i>Khalid Tawfiq 
					Abdul Rasoul</i><b>, Deputy Executive Director of Sharq Sohar 
					Group, in an interview with 
                    </b><i>Muhammed Nafie</i><b>. Excerpts:<br>
                    </b></font><font face="Verdana" size="1"><br>
                    <b>
                    <img border="0" src="images/jan_CP1.jpg" align="left" hspace="12" vspace="6" width="150" height="184" style="border: 2px solid #C0C0C0">Can 
					you give us a brief background of Sharq Sohar?</b><br>
					Sharq Sohar Steel Rolling Mills (SSSRM) set up its plant in 
					1997 and started operation in 1999. In 2000, the company 
					launched supply of its products in local market. Initially 
					we had exported steel to Saudi Arabia and Iran; but later, 
					when the demand in local market increased, we stopped 
					exporting to foreign countries and started to focus on Omani 
					market only. Located in Sohar Industrial, SSSRM is now the 
					largest producer of reinforcement bars in &#8232;the Sultanate.<b><br>
					</b><br>
					<b>What is your production capacity and how do you meet the 
					demand in local market?</b><br>
					Currently we are producing around 250,000 tonnes of high 
					strength steel reinforcement bars per year, with a product 
					mix of 8 mm to 32 mm ((8,10,12,14,16,18,20,22,25 and 32 mm) 
					high strength reinforcement bars confirming to BS, ASTM and 
					SASO standards. The demand for steel bars in local market 
					was estimated at 600,000 tonnes per year. It means we are 
					supplying around 40-50 per cent of the total demand of the 
					market. Though we are capable of achieving a total annual 
					production capacity of 300,000 tonnes with certain 
					adjustments in our melt, we are not doing it currently.<br>
					<br>
					<b>What are the main features of your products? </b><br>
					Our bars are made from fully killed steel. The main 
					characteristics of our products include lower carbon 
					content, higher yield strength, tensile strength, percentage 
					elongation, very good corrosion resistance properties, easy 
					bendability and weldability and excellent ductility which 
					ensure economy and safety of use. Their higher fatigue 
					strength makes them suitable for all types of structures and 
					foundations subject to dynamic and seismic loading. The 
					Temperit (water quenching) process eliminates all 
					undesirable effects of conventional cold twisted deformed 
					bars and higher strength is attained even with lower carbon 
					content.<br>
					<br>
					<b>Can you explain about your epoxy coating plant and weld 
					mesh plant?</b><br>
					We have a fusion bonded Epoxy coating plant to cater to the 
					need of construction industry which looks for coated rebars 
					to prevent the rusting and corrosion. People who want to 
					build their houses and buildings next to the sea wants 
					coated bars.<br>
					<br>
					Another value added product is welded steel mesh produced in 
					our Weld mesh plant. Both these units are working under M/s 
					MECI, situated in Sohar Industrial estate.<br>
					<br>
					<b>What are your quality control measures?</b> <br>
					We are very particular about ensuring the quality of our 
					products and we follow safe and environment friendly 
					production practices. We are committed to produce and supply 
					bar products as per international standards like BS / SASO / 
					ASTM and strictly comply with all quality requirements from 
					billet procurement stage right through production, quality 
					control / testing and product delivery.<br>
					<br>
					The products are tested in regular intervals during 
					production. Samples are taken every 15 minutes for checking 
					cross section, visual defects and sectional weight. For 
					every 30 tons, one sample is taken for testing the 
					mechanical properties like yield strength, tensile strength, 
					elongation and bend / re-bend test. The quality control lab 
					is equipped with state of the art facilities like computer 
					controlled tensile testing machine. OSCAM bending machine, 
					consisting of an in-built electronic programmer, is used for 
					bending and rebending of the bar for bend test. Angles from 
					0 degree to 180 degree for bending and rebending are set 
					through the electronic programmer. <br>
					<br>
					Various international certifications we achieved testify to 
					this fact. As I mentioned, our bars conform to the latest BS 
					standard. They also have prestigious CARES certification by 
					M/s UKAS, UK for the complete product range from 8 mm to 32 
					mm. Besides, Sharq Sohar Steel Mill is accredited with ISO 
					9001:2008 certification for the entire unit including 
					testing facilities.<br>
					<br>
					<b>What about your latest backward integration, Sohar Steel?</b><br>
					Sohar Steel, our unit for producing continuous cast steel 
					billets is located at Sohar Port industrial area (SIPC). The 
					plant is capable of producing 250,000 MT of steel billets 
					annually which are meant for consumption in Sharq Sohar, 
					thereby reducing our dependence on imported raw materials.<br>
					<br>
					Sohar Steel is equipped with modernised wet lab facilities 
					and most modern type spectrometer through which strict 
					quality control measures are practiced to achieve the 
					specified product quality standards. Sohar Steel considers 
					conservation of nature as one of its prime responsibilities 
					and it has adopted the best available dust collection 
					technology to limit the emission.<br>
					<b><br>
					How competitive is the Omani steel industry and how do you 
					look forward?</b><br>
					Steel is one of the essential commodities and we cannot do 
					without it. In Oman, the economic diversification plans as 
					part of Vision 2020 aim at 6.5 per cent GDP growth with the 
					contribution of other vital sectors to the economy. Steel 
					industry also plays a crucial role. We do not see any major 
					downturn in the industry in the forthcoming years. A lot of 
					projects are under construction and many others are in the 
					pipeline. We are the largest producer of steel bars in the 
					country.<br>
					<br>
					We are also looking for further expansion of our plants so 
					that we will be able to meet the increasing demand in the 
					local market. Sohar Steel is up for an important expansion 
					which will enable us to increase the production capacity to 
					300, 000- 600,000 tones in the future.<br>
