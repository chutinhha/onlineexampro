				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1"><br>
                </font><b><font face="Verdana" size="4" color="#C00000">Breaking 
				confines<br>
                </font></b><font face="Verdana" size="1"><br>
                </font><font face="Verdana" size="2"><b>Outward Bound Oman is 
				introducing corporate Oman to a unique concept – the use of 
				outdoors for teaching workplace skills. A report by </b><i>Mayank Singh</i></font><br>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/jan_CRS1.jpg" width="150" height="191" align="left" style="border: 1px solid #C0C0C0; ">An oft-repeated complain of 
				employers and HR managers is that there is a gap between 
				education and the needs of a workplace. Employees, both 
				prospective and present, are often charged with lacking skills 
				like working in teams, communication gap and so on. Though 
				organisations try to narrow this skill gap by taking them for 
				training courses, the results turn out to be anywhere from 
				average to disappointing. The use of outdoors for skill building 
				is an alternative approach being explored by various 
				organisations. Says Mark Evans, general manager, Outward Bound 
				(OB) Oman, “Using the outdoors as a learning curve has enormous 
				benefits.” <br>
				<br>
				Launched in May 2009, OB or Tahaddi, which in Arabic means 
				‘challenge’, has conducted 30 courses from September 2009-2010. 
				Around 400 executives participated in these courses with 70 per 
				cent of them being from Oman. “We thought we would do 20 
				courses, but ended up doing 30,” says Evans. OB expects to do 33 
				courses during 2010-11 (September to August).<br>
				<br>
				The courses are tailor-made to suit the needs of individual 
				companies. For example in May 2010, a three-day course was 
				designed for 14 employees of Oman LNG. The brief was to 
				facilitate a cross departmental dialogue. The group was taken to 
				Jebel Akhdar and trained in leadership and management skills. 
				The CEO of the company, Dr Brian Buckley, himself participated 
				in the course. Says Evans, “In an environment where the 
				surroundings are continually changing everyone has to evaluate 
				risk, think and cooperate with each other.”<br>
				<br>
				The group is exposed to different leadership styles. For 
				example, for the first three hours a leader may be asked to lead 
				the group in an autocratic style. Other members of the group are 
				given a chance to talk frankly about the leadership style later 
				on. This is then followed by other managerial styles – 
				democratic, consultative etc and the strengths and weaknesses of 
				each of the these styles are discussed individually. <br>
				Mahmood Al-Hadidi who participated in the course from Oman LNG 
				says, “Knowing one’s strengths and areas of development can only 
				be realised under real time pressure and demanding situations 
				and this programme gave us exactly that. The beauty of OB 
				experimental learning is that one actually experiences what one 
				is imbibing, rather than sitting in a classroom imagining what 
				it looks like. The hidden potential that some of us had was 
				unleashed due to the nature of the programme and the way it was 
				designed.”<br>
				<br>
				Richard M. Preece, CEO, Oman Methanol Company (OMC) shares the 
				feedback he got from the OMC organiser who participated in one 
				of OB’s programmes, “The course was a unique experience for the 
				10 OMC staff who spent three days and two nights outdoors. The 
				challenge of living in an unusual environment wherein everyone 
				had to live with a little food and other amenities made us aware 
				of the need to help other team members. The intellectual 
				exercises and discussions were a special experience as they 
				challenged our imagination. Team work, being organised and good 
				communication were among the great lessons that the team members 
				learnt besides planning and self dependence.” <br>
				<br>
				</font><font face="Verdana" size="2"><b>Customised courses</b></font></p>
				<p align="center" style="margin: 10px; "> 
				<img border="0" src="images/jan_CRS2.jpg" width="500" height="164" style="border: 1px solid #C0C0C0"><font face="Verdana" size="1"><br>
				&nbsp;</font></p>
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">OB courses are not just limited to corporate issues, but can be 
				used to inculcate other values as well. When Caledonian College 
				of Engineering wanted its students to be aware of the 
				environmental impact of their future careers, it knocked on OB 
				doors. Students were asked searching questions like, ‘If you 
				were working for a construction company and had to build a 
				five-star hotel, what would its implications be for the 
				environment? <br>
				<br>
				A number of companies like Oman LNG, Sohar Aluminium, Oman 
				Methanol have used courses being offered by OB to inculcate a 
				range of values like team work, environment awareness, road 
				safety etc. Apart from corporates the organisation is also 
				working with a number of government schools to transfer life 
				skills to students. The organisation has found support from the 
				private sector in such endeavours. Renaissance Services is 
				supporting the programme for six schools this year, Omran has 
				pledged support for four schools and Oman LNG is helping six 
				government schools to avail of OB courses. <br>
				<br>
				Realising the importance of the initiative, a number of other 
				companies have extended their help to OB. For example, the W J 
				Towell group has given it office space in Madinat as Sultan 
				Qaboos, Oman Methanol company provides fuel to OB and Massirah 
				International lets it store its equipment in one of its 
				warehouses in Ghala. The Suhail Bahwan Group, Denton Wilde Sapte, 
				Shell Oman and BG Oman got together and founded OB Oman in 2009. 
				Says Evans, “These companies felt that there was an opportunity 
				to do something different in Oman.” The Sultanate is the first 
				country in the region to start such a programme. <br>
				<br>
				Internationally OB started in Great Britain in 1941 during the 
				Second World War. At a time when Britain was losing a lot of 
				people at sea, the need was felt for a physically and mentally 
				challenging programme that would equip young people to take on 
				wartime challenges. In 2010 there were 30 OB schools across the 
				globe. OB is headquartered in the US and has a repertoire of 
				over 20,000 courses. This serves as a good reference point for 
				others to draw upon. Local offshoots can customise these courses 
				to suit the needs of their clients and cultures. For example, if 
				the brief is to inculcate better communication skills, align 
				people to a company’s corporate vision or to foster time 
				management skills then courses can be tailored to suit these 
				individual needs. <br>
				<br>
				</font><font face="Verdana" size="2"><b>The mechanics of a 
				course</b></font><font face="Verdana" size="1"><br>
				<br>
				OB Courses usually span over Monday, Tuesday and Wednesday, 
				while the team prepares for the course on Saturday and Sundays. 
				A minimum of six people are required for a course and the 
				maximum number of people that are taken aboard for a course is 
				18. When it comes to schools, OB works closely with the Ministry 
				of Education. The Ministry identifies the schools that it wants 
				OB to work with and introduces it to the concerned schools. Once 
				the introduction has been done, then a member of OB goes to the 
				respective school and explains the various requirements to its 
				students and teachers. For girls, there are women instructors 
				and trainers. <br>
				<br>
				During the first year in Oman OB was focused on designing 
				courses and refining it. With the courses in place, the 
				programme is now focusing on measuring the impact of its 
				courses. Says Evans, “Our course ends with each participant 
				evaluating themselves on various parameters like leadership, 
				problem solving, teamwork, leadership, time management and 
				environment awareness.” The CEO’s of participating companies are 
				invited to come on the final day of the course and each 
				individual is asked to give a presentation on what he learnt and 
				how this knowledge could be transferred to his workplace. <br>
				<br>
				The feedback to OB has been encouraging. Evans says with a tinge 
				of pride that he has rarely gone knocking on doors for support 
				or participation. “Our alumni are our best ambassadors.” Having 
				got a good start, OB is keen to build upon the strong foundation 
				that it has established. “We want to grow steadily and are under 
				no pressure to stretch ourselves,” says Evans. As they say, well 
				begun is half done.</font><br>
