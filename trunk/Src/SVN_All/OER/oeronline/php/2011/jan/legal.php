                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					<br>
					The complexity of jurisdiction<br>
					</font><font face="Verdana" size="2"><br>
					An issue which is often overlooked when drafting a contract 
					is the clause about jurisdiction<br>
					<br>
					</font></b><font face="Verdana" size="1">Jurisdiction” 
					refers to the entity which will hear any dispute between the 
					two parties. Dispute resolution is normally performed by the 
					courts or via an arbitration panel. However, it is not 
					uncommon to read legally inaccurate contractual provisions, 
					which state something such as: “The Oman Courts will settle 
					any dispute via arbitration.” Sentences such as these can 
					lead to confusion as regards what the parties’ intention 
					actually was; one party will argue that it is clear that the 
					Oman Courts have jurisdiction to hear the dispute, whereas 
					the other party might argue that the Oman Courts cannot hear 
					the dispute, as the Oman Courts are only empowered by the 
					clause to decide who will act as arbitrator. The confusion 
					arises because resolution via the courts is something 
					different to resolution via arbitration.<br>
					<br>
					Strictly speaking, when the parties agree in a contract that 
					the Courts of a certain country will have jurisdiction, the 
					ensuing case is known as “litigation” and the ultimate 
					decision by the judges is known as the Court’s “judgment”.<br>
					<br>
					In contrast, when the parties agree in a contract that the 
					dispute will be settled by arbitration, the ensuing 
					proceedings are known as “arbitral proceedings”, and the 
					ultimate decision of the arbitrator(s) is known as the 
					“arbitral award”.<br>
					<br>
					It is vital, before entering into a contract, to think very 
					carefully about the entity which would have jurisdiction to 
					hear any later dispute. Let us say that an Omani entity is 
					considering entering into a contractual relationship with an 
					English company. The Omani company may want the jurisdiction 
					to be “Omani Courts”, on the basis that the procedures of 
					the courts here are well-known and familiar to that entity. 
					However, what happens if the two entities later have a 
					dispute which is settled by a final, non-appealable Omani 
					court judgment? The difficulty may only be faced by the 
					Omani entity at that juncture, because obtaining a court 
					judgment is not the same as actually receiving the monetary 
					damages/compensation in your company’s bank account.<br>
					<br>
					The truth is that one has to think, before even entering a 
					contract, as to what might happen if you have a dispute at a 
					later date with your contractual counter-party. Let us say, 
					in the example above, that the English company simply 
					decides not to abide by the final Omani court judgment and, 
					frankly, tries to disregard and avoid it. Let us also 
					suppose that the English company has no assets in Oman and 
					no presence in Oman.<br>
					<br>
					The Omani company has a paper final Omani Court judgment in 
					its favour, but how will it enforce that judgment (ie get 
					the money which the English company has been ordered to 
					pay)? The fact of the matter is that the Omani company may 
					then have to start fresh court proceedings in England, where 
					the Omani court judgment may only have evidential, rather 
					than binding, weight. This could lead to extra cost and 
					years of dispute, at the end of which the Omani company may 
					not get what was decreed by the Omani Courts.<br>
					<br>
					But what would have happened if the contract in question 
					stated that any dispute between the Omani company and the 
					English entity would be settled by arbitration in Muscat? 
					The answer is that the Omani arbitral award would be 
					automatically enforceable in England against the English 
					entity. This is because both Oman and England have signed 
					the New York Convention on the recognition of foreign 
					arbitral awards. In the above specific circumstances, an 
					Omani arbitral award would have much greater value to the 
					Omani entity as compared with an Omani court judgment.<br>
					To conclude, legal advice is always recommended as regards 
					the drafting of the jurisdiction clause. The decisions made 
					pre-contract in this respect could be crucial to your 
					business in a few years’ time.<br>
