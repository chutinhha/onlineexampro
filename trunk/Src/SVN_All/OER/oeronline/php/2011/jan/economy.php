                  <p style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Oman-US FTA – 
					promising start</font></b></p>
				<p style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
                    Bilateral agreements may well represent one of the most 
					effective ways to promote economic reforms throughout the 
					region<br>
					</b></font><b><font face="Verdana" size="1"><br>
					</font>
					</b><font face="Verdana" size="1">According Official 
					relations between the US and Oman stretch back nearly two 
					hundred years, with the “Treaty of Amity and Commerce” inked 
					in 1833 marking the first bilateral accord between the US 
					and an Arab state. However, both diplomatically and 
					economically – as if the two were ever separate – Oman has 
					been most engaged with neighbours closer to home. It also 
					has extremely strong ties with the UK. In 2008, non-oil 
					exports making their way from Oman to the UK totaled RO46mn. 
					Those headed to the US, with an economy more than seven 
					times as large, were not much bigger, at RO72mn.<br>
					<br>
					Winds of change have been in the air since 2006, when the US 
					and Oman committed to working towards a free trade 
					agreement. The treaty came into effect in 2009, allowing 
					immediate duty-free access to most commercial, industrial, 
					and agricultural products for both countries, with a 
					commitment to phasing out existing tariffs over the 
					subsequent 10 years.<br>
					<br>
					By almost all accounts, the treaty has been a success. With 
					the second anniversary of the treaty’s inauguration fast 
					approaching, has the FTA achieved the ends it set out to 
					address? And are the changes it has brought here to stay?</font><font face="Verdana" size="2"><b><br>
					</b></font><font face="Verdana" size="1"><br>
					Generally, yes. The agreement was the result of years of 
					diplomatic negotiations and its wide-reaching reforms show 
					the government’s commitment to integrating Oman into the 
					global economy. Trade is rising, especially in the westward 
					direction: Omani non-oil exports to the US have increased 
					over 300 per cent, jumping from RO13.2mn to RO53.6mn in the 
					first half of 2010 as compared to the same period in 2009. 
					In addition, the FTA has granted US companies the right to 
					establish and own businesses in Oman without a local partner 
					in most sectors, apart from legal services. This is a 
					privilege that had already been extended to other members of 
					the GCC, however, it remains a unique opportunity for the US 
					when compared to Oman’s other major trading partners.</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><br>
					<br>
					</font><b><font face="Verdana" size="4" color="#C00000">
					Advantages galore</font></b></p>
					<p align="center" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><br>
					<br>
					<img border="0" src="images/jan_ecnomy.jpg" width="400" height="252" style="border: 1px solid #000000"></font></p>
					<p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
					<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">US Companies also benefit from lower Omanisation 
					requirements, with only 80 per cent of the employees of a US 
					company in the Sultanate required to be Omani nationals. 
					Furthermore, this threshold excludes managers, members of 
					the board of directors, and “specialty personnel” – broadly 
					defined in the treaty as “persons who are employed to use 
					their expert or proprietary knowledge of a covered 
					investment’s services, equipment, techniques, or management 
					and may include, but are not limited to, members of licensed 
					professions” – granting in practice significant licence to 
					hire on the international market.<br>
					<br>
					Importantly, the benefits of the US-Oman FTA have not 
					accrued only to the two countries involved. Indeed, the FTA 
					is seen as a one of the key driving forces in Oman’s ongoing 
					economic liberalisation, as it has encouraged greater 
					transparency and adherence to international standards. For 
					example, a 2006 revision to the existing Labour Law, drawn 
					up as a precondition to signing the FTA, requires employers 
					to engage in collective bargaining with their employees over 
					terms and conditions of employment, prohibits employers from 
					firing employees that engage in union activity and increases 
					penalties for hiring underage labour. A guarantee of 
					intellectual property rights and a secure legal framework 
					for investments have also been put in place. Furthermore, 
					tenders for public works must now be advertised in the 
					press, allowing all international investors to profit from 
					the agreement. These developments have rendered Oman a more 
					attractive investment destination both for US investors and 
					the wider world.<br>
					<br>
					However, the economic liberalisation of Oman has never been 
					the sole intent of the US-Oman FTA. The agreement marks the 
					fourth FTA signed by the US with an Arabian country, 
					following those reached with Jordan, Morocco, and Bahrain. 
					As originally conceived, the US-Oman FTA, like the others, 
					was intended as an important stepping stone towards an 
					eventual Middle East Free Trade Agreement.<br>
					<br>
					While nearly everyone agrees that this is a good goal in 
					theory, differences have arisen as to the path Middle 
					Eastern countries should take to achieve this regional 
					agreement. Many see bilateral FTAs as effective building 
					blocks, since they encourage standardisation. Others, 
					however, believe these agreements undermine intra-regional 
					cooperation.<br>
					<br>
					Bahrain was the first member of the GCC to complete an FTA 
					with the US, in 2004. Soon after, Reuters reported that 
					Bahrain was coming under fire from its neighbours in the 
					Gulf for concluding an agreement which they claimed was in 
					violation of the GCC economic agreement, which forbids 
					“granting preferential treatment exceeding that granted to 
					member states”. The GCC member states had expressed a desire 
					to form a common Customs Union in 2003 that would set a 
					common tariff of 5 per cent on external trade. The FTA, 
					which abolished external tariffs in US-Bahrain trade, would 
					seem to run counter to this agreement.<br>
					<br>
					However, in practice, the GCC Customs Union was still far 
					from implementation, and indeed remains so today. Though the 
					legal framework for the Customs Union was laid out and 
					ratified in 2003, implementing the practices outlined on 
					paper has proved to be more of a challenge than expected.
					<br>
					<br>
					Key stipulations of the Customs Union included the abolition 
					of all internal tariffs among the GCC member countries, and 
					a fair distribution of the customs revenue derived from 
					external trade. An initial three-year transition period was 
					provided for in the 2003 agreement, and subsequently 
					extended in 2008. Disagreements over border controls and 
					revenue distribution have continued to stall progress. A 
					decision to delay the full implementation of the Customs 
					Union a further two years was announced at the conclusion of 
					a meeting of GCC ministers of foreign affairs, finance and 
					economy held in Jeddah this September. While this 
					disappointed some, the statement was tempered by the 
					parallel announcement that automated customs clearance 
					systems will be rolled out soon to help combat delays.<br>
					<br>
					Proponents of the GCC Customs Union maintain that a unified 
					economic bloc would boost the standing of its member 
					countries within the world trade community. However, as many 
					have noticed, bilateral trade agreements like the US-Oman 
					FTA serve the same purpose. Given the continued difficulty 
					of implementing a unified Customs Union throughout the GCC, 
					bilateral agreements may well represent one of the most 
					effective ways to promote economic reform throughout the 
					region.<br>
