						<td valign="top">
						<p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
                Two 
				of the most striking features of M37S are Blind Spot 
				Intervention (BSI) system and Lane Departure Prevention (LDP). 
				The BSI system uses sensors to warn the driver against a vehicle 
				that is approaching in his/her blind spot and helps the vehicle 
				to avoid shifting if the driver tries to change lanes. The 
				system applies opposite-side brakes to guide the vehicle remain 
				in its lane, to reduce the risk of an accident. To override the 
				system in an emergency situation, harder input on the steering 
				wheel will allow the driver to move into the occupied lane. The 
				LDP is a tool that senses unwanted and potentially dangerous 
				drift between lanes while driving and automatically compensates 
				to ease the car back between the lines.<br>
				<br>
				With a price tag of RO24,000 approximately, M37S has the 
				wherewithal to be a desirable sedan for the discerning few. 
				Infiniti offers two more options -- Excellence and Luxury apart 
				from Sports – in the M37 range. In addition, the auto major 
				offers M56 which is fitted with 5.6 litre V8 engine for those 
				who are looking for an upgraded version in the same M range.<br>
