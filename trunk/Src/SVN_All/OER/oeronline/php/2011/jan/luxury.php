		        <td valign="top" width="100%"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					<br>
					Timely moves<br>
					</font><font face="Verdana" size="2"><br>
					Perrelet prides itself in being the inventor of the 
					automatic watch. In contemporary times, the watchmaker has 
					become famous for its double rotor pieces. Fausto Salvi, 
					CEO, Perrelet, who was in Oman, recently talks about the 
					brand in an exclusive interview with Visvas Paul D Karra. 
					Excerpts.<br>
					<br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/jan_lexury1.jpg" hspace="9" vspace="5" style="border: 2px solid #C0C0C0; " align="left" width="150" height="204"></font></b><font face="Verdana" size="1"><b>How 
					long you been in the watch industry and what is your 
					impression of Perrelet?</b><br>
					I joined the Perrelet company in March 2009 as CEO. Before 
					that my experience in the watch industry was with Hugo Boss, 
					Gucci and Ebel brands. My total watch industry experience is 
					over 17 years.<br>
					<br>
					When I arrived in Perrelet, it was a difficult economic 
					situation worldwide. The brand was also going through tough 
					times. For us it was a big opportunity to review the brand 
					strategy and it was a good moment to rethink about the 
					repositioning of the brand in terms of pricing, in terms of 
					collection and the distribution network. The first thing was 
					to review and clean the collection and propose a whole new 
					range of products in order to make it more dynamic.<br>
					<br>
					The second main task was the price positioning done by 
					reducing between 10 and 30 per cent in order to be more 
					competitive. We were able to decrease the price because we 
					converted most of our manufacturing process to an industrial 
					scale. Thus the price of the components was brought down 
					ultimately bringing down the cost of the final product. <br>
					<br>
					Thirdly, we reworked on our dist ribution networks across 
					the world. By this we mean, we closed in many countries in 
					2009, because we were not with the correct partner and we 
					reopened countries like Hong Kong. We are also looking for 
					new partners in Latin America and Europe.<br>
					<br>
					<b>Was it due to the recession that you closed your networks 
					in some countries?</b><br>
					We closed down in around 10 countries but it was not due to 
					recession. It was because our partners were not able to 
					develop the brand correctly. It is a young brand, no doubt, 
					and we are still small producing around 4,500 pieces per 
					year.<br>
					However, to build and develop the brand in the price range 
					we are proposing, we needed people whounderstood the brand, 
					and had correct point of sales to sell qualitative brands. 
					Managing a brand whose retail price ranges from RO8000 and 
					above is different from a brand which costs RO800.<br>
					<br>
					<b>What is your opinion about your distribution partner in 
					Oman?</b><br>
					Our distribution partner (Gallery Argan) is very dynamic. He 
					already has a network of point of sales and he has good 
					plans for the future. This is very important for the future. 
					Secondly, the market is small and considering the presence 
					we already have, we are happy to have three points of sales 
					in Oman.<br>
					<br>
					<b>Can you define the function of a watch?</b><br>
					There is a vast difference in how a watch is perceived by 
					men and women. To begin with, a watch is just a timepiece. 
					Secondly, while women can show-off accessories like a 
					handbag, a lighter, jewellery or her dress, a man does not 
					have so many choices to show. A watch is more or less is one 
					of the unique things he can show off. The shape, the design 
					and the price positioning of the watch reveal your status. 
					&#8232;Therefore, it is more than a watch for a man.<br>
					<br>
					<b>What is the uniqueness of a Perrelet watch?</b><br>
					The double rotor. The rotor at the top is linked to the 
					rotor at the bottom. From a technical point of view, it will 
					help to rewind the watch faster but also it will give you an 
					animation feeling in the dial. We are unique with the 
					diamond flower dial family for the ladies, where we have the 
					rotor at the top in the shape of the lotus flower and this 
					rotor is moving when you move your wrist.<br>
					<br>
					For men, the rotor is titanium rotor. Another more classic 
					but with the same rotor is the turbine rotor, which moves 
					faster and gives the impression of the turbine of an 
					airplane. All the communication is based on the double rotor 
					and besides this, Perrelet is considered the brand of 
					specialities. We do not consider Perrelet as the brand of 
					complications, because we have very less complications, so 
					we prefer them to call them specialities. For example, we 
					are the first with the double rotor, besides this we have 
					the jumping hour, then we have the central moon faced dial. 
					Apart from this, we have the regular retrograde, colour code 
					and big date.<br>
					<br>
					Please describe the manufacturing process of a Perrelet.<br>
					<br>
					It all begins with a drawing. Based on the drawing, we do 
					prototypes in wax, so you can see the shape and the volume. 
					Then we work on the technicals. If we are happy with the 
					technicals, we give the watch to the manufacturer of cases 
					who will produce the samples, then we do the same thing for 
					the dials, the strap. The whole watch is built around the 
					movement because it’s the movement which determines the size 
					and volume of the watch. When everything is approved, we 
					send it for mass production.<br>
					<br>
					<b>What are your impressions of the watch industry after 17 
					years of experience?</b><br>
					We should be on our toes to follow the trends and requests 
					of clients. This is not easy because the notable thing is 
					trends keep changing very quickly. If you look at the cycle 
					of producing a watch, which is 18-24 months, the taste of 
					the client would have changed quickly, therefore it is a 
					challenge to introduce the right product into the market at 
					the right time.<br>
					<br>
					Then there is the issue of price. You have a lot of brands 
					that are dying because if you had a client who was buying 
					two or three watches a year, today he is buying only one and 
					he will go to the brands that are well established or the 
					more classic watch brands. So things are changing fast and 
					we have to be very careful. But we believe that we have a 
					safe proposition in a brand like Perrelet because we not 
					only have very trendy products like the turbine but also 
					more classic products with the specialities.<br>
					<br>
					<b>Do you feel threatened by the digital watches? What is 
					the future of watches?</b><br>
					We have to stick to our core brands and business. For sure 
					there will be an evolution. Many brands are working on 
					improving the existing movements and also coming up with 
					novelties so it will depend upon the requests of the final 
					consumer. As you can see, till today, the final consumer is 
					still interested in the classical, safe automatic watches.<br>
					<br>
					<b>Which is the biggest market for Perrelet?</b><br>
					Our biggest market is the US. Soon we will achieve 50 points 
					of sales in that country. What that also means is that we 
					are beginning to make an impact on the market. This also 
					means we are starting to become an interesting brand. <br>
					<br>
