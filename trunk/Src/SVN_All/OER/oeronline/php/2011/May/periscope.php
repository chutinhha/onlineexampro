						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Qatari banks on the go </font></b><p class="style15">
				<strong><span class="style14">Qatar’s fabulous LNG wealth 
				banking and industrial assets and minimal exposure to political 
				risk make Doha an ideal GCC stock market<br>
&nbsp;</span></strong><p align="justify" class="style13">
				The Qatari economy can benefit from the spike in crude oil, 
				natural gas and LNG prices since the escalation of the Libyan 
				civil war, where the emirate’s air force helps NATO enforce the 
				no fly zone. Qatar is the fastest growing economy in the Middle 
				East and the world thanks to rapid bank loan growth and Doha’s 
				QR100bn government stimulus programme. Even in 2009, when so 
				many economies in the GCC were devastated by property crashes, 
				corporate bankruptcies and the post-Lehman credit crunch, Qatar 
				delivered 8 per cent GDP growth, a pipe dream for most Arab 
				economies. The Doha Stock Exchange also delivered the strongest 
				corporate profits growth in the Arab world, even though Qatar 
				experienced a plunge in local property prices and rents. 
				However, Qatari companies can and do enjoy access to the 
				international capital markets and are thus able to expand capex 
				and credit growth.<br>
				<br>
				The Qatari banking system is among the most highly capitalised 
				and profitable in the Middle East, with a 16 per cent capital 
				adequacy ratio (QAR) and 25 per cent bank earnings growth, the 
				highest in the GCC. More significantly, Qatari banking has the 
				lowest non performing loan (NPL) ratios in the GCC, a mere 1.7 
				per cent. Qatar’s budget surplus will be QR22bn in 2011-13. <br>
				<br>
				<b>Untouched by unrest</b><br>
				The political unrest in the Arab world has left Qatar untouched, 
				though the economy is vulnerable to another drop in property 
				prices or an ice age in the global bond, syndicated loan or 
				project finance markets. The Japanese nuclear disaster and the 
				bloodbath in Libya has led to a significant rise in spot LNG 
				prices, a windfall for Qatar, which owns 14 per cent of the 
				world’s proven gas reserves. For an investor, Qatar’s fabulous 
				LNG wealth banking and industrial assets and minimal exposure to 
				political risk make Doha an ideal GCC stock market.<br>
				<br>
				Qatar National Bank (QNB) is the largest flagship bank of the 
				emirate, the de facto banker to the government with the highest 
				economies of scale/net profits in the banking system. At a time 
				when Middle East banks face funding constraints in the 
				international capital markets, QNB benefits from its sovereign 
				ownership, its clear dominance of public sector government and 
				its 15 per cent Basle Tier One capital ratio. I expect both 
				earnings growth and net interest rate margins to expand as QNB 
				can well deliver 15-20 per cent loan growth in the next three 
				years due to its role as a project financier to the Qatari 
				government at a time of rising crude oil and LNG prices. After 
				all, the QIA, one of the Arab world’s most successful sovereign 
				wealth funds, has a 50 per cent stake in QNB. A collapse in LNG 
				prices and the impact of unrest on its Arab subsidiaries are the 
				major macro risks <br>
				for QNB.<br>
				<br>
				Qatar Islamic Bank (QIB) is the oldest leading Sharia-compliant 
				bank in Qatar, with an unassailable market share of the 
				one-third of all Islamic loans in the banking system. Sharia-compliant 
				finance is the growth area of Qatari banking, with credit growth 
				rates as high as 40-45 per cent per annum. The Central Bank has 
				ordered conventional banks in Qatar to divest of their Islamic 
				banking units. QIB, where the Qatari sovereign fund has a 
				strategic stake, is not expensive at 11 times earnings and 2 
				times book value. Both QNB and QIB are excellent proxies for 
				investing in Qatari economic growth and consumer spending in the 
				emirate which boasts the world’s highest per capital income.<br>
				<br>
				The three metrics that will be mission critical to successful 
				investing in Qatari banking will be GDP growth, loan growth, NPL 
				trends and EPS growth. <br>
				<br>
				Qatar is the highest growth economy in MENA and benefits from 
				capital inflows from Arab countries in the throes of civil war, 
				revolution or sectarian schisms. Qatari government spending, 
				25-30 per cent of GDP, alone makes bank loan growth and EPS 
				growth inevitable in an economy where credit penetration is 
				still low, at two thirds of nominal GDP, far below the GCC 
				average. Qatari banks offer the best risk reward calculus in 
				MENA.</td>
