						<img border="0" src="images/golfupdate01-may11.jpg" width="523" height="303" vspace="10"><p class="style15">
				<strong><span class="style14">The golf has entered the 
				post-Tiger Woods era with Martin Kaymer, the fluid-swinging 
				German, edging out Lee Westwood as the number one golfer in the 
				world<br>
				</span></strong><p align="justify" class="style13">
				German 
				professional golfer Martin Kaymer became the No. 1 ranked golfer 
				in the official World golf rankings in February 2011 after his 
				runner-up finish in the WGC-Accenture Match Play Championship. 
				It marked a new phase of golf, where the top ranking gets passed 
				around a handful of players the way box-office grosses designate 
				a new No. 1 movie almost every week.<br>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo11.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>At 26, Kaymer is the second-youngest player to capture the top 
				ranking since Woods did so at age 21 in 1997. But he was also 
				the first player younger than Woods to pass Tiger in the 
				rankings, which he did earlier this year. Kaymer, who was born 
				in Düsseldorf, Germany, in 1984 turned professional in 2005. A 
				member of the European Tour, he has won nine tournaments on the 
				tour including four in 2010 to win the Race to Dubai, formerly 
				the Order of Merit, for the first time.<br>
				<br>
				After missing the cut at the Masters, Kaymer performed very well 
				in the 2010 majors. He finished in a tie for eighth at the US 
				Open and then tied for seventh at the Open Championship after 
				starting the final round in third. On 15 August 2010, Kaymer won 
				the PGA Championship at Whistling Straits for his first major 
				championship-a victory which made him only the second German to 
				win a major championship, after Bernhard Langer. Finishing 
				regulation play in a two-way tie at 11 under par, he defeated 
				Bubba Watson in a three-hole playoff. In 2010, Kaymer and Graeme 
				McDowell shared the European Tour Golfer of the Year award.<br>
				<br>
				Kaymer was a member of the winning European Ryder Cup team. He 
				won both four-balls (partnered with Westwood and Poulter), 
				halved his foursome and lost his singles match. A week later he 
				won the Alfred Dunhill Links Championship at St Andrews with 
				Danny Willett coming in three strokes behind. He was the first 
				player since Tiger Woods in 2006 to win three successive 
				tournaments in a year and the first European to achieve this 
				since Nick Faldo in 1989. <br>
				<br>
				Entering the 2011 season, Kaymer turned down a chance to become 
				a full PGA Tour member; he had become eligible with his win in 
				the 2010 PGA Championship. He stated he would concentrate on the 
				European Tour for 2011, but would play several US events as 
				well.<br>
				<br>
				In January 2011, Kaymer claimed his third Abu Dhabi HSBC Golf 
				Championship title in four years and displaced Tiger Woods as 
				number two in the world rankings. After his runner-up finish at 
				the 2011 WGC-Accenture Match Play Championship, Kaymer overtook 
				Lee Westwood as the number one golfer in the world, making him 
				only the second German (after Bernhard Langer) to be the 
				top-ranked golfer in the world.</td>
