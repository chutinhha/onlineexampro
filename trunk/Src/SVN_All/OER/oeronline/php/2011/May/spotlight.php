				<b><font size="2"><font color="#0000FF">WHAT MATTERS MOST </font>
				<br>
				<br>
				It is important to know the basics about air conditioners before 
				purchasing one. Here are a few tips to understand the 
				differences between the various choices available <br>
				</font></b><br>
				<br>
				One of the most common type of air conditioner that we come 
				across on a day-to-day basis is the room air conditioner. This 
				is an enclosed cooling unit that can be mounted in a window, 
				through a wall or in a portable cabinet on a floor near a 
				window. Its job is to cool the air in a small, confined area as 
				directed by the unit’s controls. Some window air conditioners 
				have other functions as well. For example, reverse intake air 
				conditioners also work as heaters, making them a home appliance 
				that is useful nearly year-round, not that it would be of much 
				use here.<br>
				<br>
				Window air conditioners work similarly to central air 
				conditioning systems, but are contained in one compact unit. 
				When the room air conditioner unit is on, the condenser pulls in 
				the refrigerant gas and pressurises it, raising the temperature 
				of the gas. The heated high-pressure gas then travels to the 
				condenser coils on the outdoor side of the unit where fins 
				distribute the collected heat. The cooled gas then condenses 
				into a liquid that moves indoor to the evaporator coils where it 
				absorbs heat from the room. A fan moves the air through the unit 
				and expels the cooled air into the room. <br>
				<br>
				For the most part split-unit air conditioners are almost the 
				same as window air conditioners, their primary difference being 
				that the cold side, consisting of the expansion valve and the 
				cold coil, is split from the hot side of the system, which is 
				placed outside the building. These are more effective at cooling 
				larger rooms that basic window ACs.<br>
				<br>
				A thermostat controls the unit’s operation. Many units now have 
				advanced electronic controls that can adjust one degree at a 
				time, rather than the primitive “cool” or “cooler” dial settings 
				that were once common. The electronic controls also may regulate 
				the fan speed, operation mode, timer and other settings. In 
				addition, many room air conditioners at a variety of price 
				points even include a remote. Split-unit air conditioners are 
				even more advanced, offering a number of features, some even 
				including bacterial control systems. <br>
				<br>
				An air conditioner unit’s cooling power is measured in British 
				Thermal Units or Btu per hour. One Btu can raise or lower the 
				temperature of one pound of water one degree Fahrenheit. All 
				window air conditioners are rated by the maximum Btu/hr. Keep in 
				mind that even the smallest window air conditioners may have 
				thousands of Btu per hour. To keep these numbers is the proper 
				perspective; remember to compare the Btu between systems. 
				Generally, the more Btu, the larger an area the air conditioner 
				can cool. <br>
				<br>
				Another important factor is quietness. A window air conditioner 
				in a bedroom, for example, should be extremely quiet so that it 
				does not interfere with sleep cycles. The quieter air 
				conditioners are well insulated to minimize rattling when the 
				fan is running. A window air conditioner that has little support 
				– or that is not installed correctly -- also can rattle within a 
				window frame.<br>
				<br>
				Warranties also vary by a fair degree on all air conditioners. 
				Many manufacturers offer a 1-year full warranty, while some 
				offer a 5-year warrant to cover the air conditioner’s sealed 
				system or major components such as the condenser, evaporator, 
				fan and controls. Many have a 5-year limited warranty on the 
				condenser (no moving parts) and 1-year warranty on everything 
				else. A few models have in-home warranties, which mean the 
				manufacturer will send a service person to your home if there 
				are problems with your air conditioner, but these warranties are 
				not common.<br>
				<br>
				To summarise, there are four key questions you have to answer 
				before purchasing an air conditioner: <br>
				<br>
				<b>HOW BIG IS THE AREA YOU WANT TO COOL AND WHERE TO PLACE THE 
				AIR CONDITIONER? <br>
				</b>Air conditioners are rated in Btu capacity. For a small room 
				(100 to 300 square feet), select an air conditioner of 5,000 to 
				6,000 Btu/hr. For a medium-size room (300 to 600 square feet) 
				choose a unit of 8,000 to 9,000 Btu/hr. For larger rooms (600 to 
				1,000 square feet) the best choice is an air conditioner of 
				10,000 to 15,000 Btu/hr. These are guidelines. If the insulation 
				in your home is minimal or the air conditioner sits in a window 
				facing the sun for most of the day, opt for a unit with higher 
				Btu. <br>
				<br>
				Also remember that these calculations are for standard room 
				heights; if the room you are cooling has a taller ceiling, 
				you’ll need about 10 percent more Btu to adequately cool the air 
				in the room. If possible, avoid installing the cooling unit in a 
				window that gets lots of sunlight as it reduces the unit’s 
				efficiency. The same goes for split-units as well. Keeping the 
				outdoor unit in mostly shaded areas improves their efficiency. 
				Keep in mind that if you buy an air conditioner with a higher 
				Btu rating than you really need, it will cycle on and off too 
				quickly. This means it will use more energy, cost more to 
				operate and remove less humidity from the air, which is a key 
				factor here in Oman. Alternatively, if you use an air 
				conditioner with a low Btu to cool a too-large room, it will run 
				continuously and cost more to operate.<br>
				<br>
				<b>CAN AN AIR CONDITIONER DO MORE THAN JUST COOL A ROOM? <br>
				</b>Yes, some also act as dehumidifiers and some do double-duty 
				as heaters. Depending on where you live, humidity may be a 
				problem or a necessity. For arid summer climates, an air 
				conditioner with a built-in humidifier can add moisture to the 
				air to make the room more comfortable. For other areas, hot days 
				are exacerbated by humidity. An air conditioner with built-in 
				dehumidifier can remove moisture from the air. Some models can 
				also serve as heaters to warm the air if needed. This means that 
				they can stay in place year-round and serve multiple purposes. 
				Keep in mind what you require and purchase one that adequately 
				serves your purpose.<br>
				<br>
				<b>HOW IMPORTANT ARE CONTROLS? </b><br>
				Older window air conditioners were controlled by a single dial 
				that was set based on an estimate of the result: Set it most of 
				the way to the left, for example, to keep the room at as cool as 
				possible. Today’s air conditioners are much smarter; you can set 
				the temperature accurately and it will maintain the air 
				temperature within one degree of that point. For convenience, 
				many window air conditioners and nearly all split-units now have 
				wireless remote controls that let you change settings -- even 
				the direction of the airflow -- from across the room. Many units 
				even have timers to program the unit to start or shut off, so 
				you can set the AC to turn on just before you get home or turn 
				off when you go to sleep. Another important feature of most air 
				conditioners is the filtration system. Nearly all units have a 
				cleanable foam filter. Some use electronic air filtration to 
				remove dust and pollens from the air. Make sure that the 
				filtration system is adequate and easy to maintain. It’s 
				important to check that extra features, such as a heater or 
				dehumidifier, have adequate and easy-to-use controls. <br>
				<br>
				<b>WHAT IS YOUR COOLING BUDGET? <br>
				</b>How much can you invest in cooling a room? If the selected 
				room only gets hot during a couple of weeks in the summer, you 
				probably want to invest in a less expensive model. If the 
				cooling season is long and the room is frequently populated, you 
				should invest in a more expensive and more powerful unit. For 
				the most part though, considering our climate, investing in a 
				good air conditioner is never a bad idea.<hr color="#C0C0C0" size="1" width="95%">
