						<span class="style27"><strong>
						<a name="TYERS">TYERS</a></strong></span><p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>
						SPOTLIGHT THE RIGHT CHOICE</strong></span><p class="style15">
				<font face="Verdana" size="1"><strong><span class="style14">
				Choosing the right tyre to suit your car and driving style is 
				the most cost effective way to improve the performance of your 
				vehicle. Your safety and driving enjoyment over the next years 
				and thousands of kilometre will be determined by this decision. 
				Here is a guide to help you take the correct decision and make 
				it just a little bit easier</span></strong></font><b><br>
				<br>
				Choosing the number</b> When the time comes for you to change 
				your tyres the very first thing that you need to decide is how 
				many tyres you really need. Since tyres affect the performance 
				of your vehicle, all four tyres should be as identical as 
				possible or handling problems may arise. Because of this, the 
				only time that you will be required to change a single tyre is 
				if your other tyres have a lot of remaining tread depth and the 
				one you need to replace has been damaged by an accident, road 
				hazard or a vandal. Even then you should replace it with a tyre 
				that exactly matches the others. Select a replacement tyre of 
				the same brand, size and speed rating.<br>
				<br>
				<img border="0" src="images/spotlight1-may2011.jpg" width="195" height="281" style="border: 1px solid #808080" align="right">In 
				most cases you will need to change tyres because they have worn 
				out. In such situations, if two of your tyres have a lot of 
				remaining tread depth, but you need to replace the other two, 
				you should replace them with a pair of tyres that come as close 
				as possible to matching your existing tyres. While identical new 
				tyres are desirable, others of the same size and type can also 
				provide good results. Only consider selecting new tyres that are 
				from the same tyre category as your existing tyres. Also, new 
				tyres should be installed on the rear axle.<br>
				<br>
				If one pair of tyres have worn faster than the others, it is 
				advisable to ask your mechanic why this has occurred. Was it 
				caused by a lack of tyre rotation, out-of-spec wheel alignment 
				or loose mechanical parts? Once the problem has been found, it 
				can be corrected before it damages your new set. Keep in mind 
				that your ultimate goal is that all of your tyres always wear 
				out at the same time so they can be replaced as a set. If all of 
				your tyres are wearing out together, you have the greatest 
				flexibility in tyre selection. If you were happy with the 
				original tyres, simply replace them. If you want longer 
				treadwear, a smoother ride or more handling, there are probably 
				tyres that will help you accomplish that. A little bit of online 
				research will go a long way in helping you decide on just the 
				right tyre to suit your needs.<br>
				<br>
				<b>The size matters<br>
				</b>When you have finally decided on the tyre that you want 
				fitted to your vehicle, the next most important ting is choosing 
				the correct size. A tyre’s first requirement is that it must be 
				able to carry the weight of your vehicle. No matter how good a 
				tyre you select, if its capabilities are “overworked” just 
				carrying the load, it will have little reserve capacity to help 
				your vehicle respond quickly in an emergency. So when you are in 
				the selection process, make certain that your new tyre’s size is 
				designed to carry the weight of your vehicle! Don’t undersize.<br>
				<br>
				The other size consideration is overall tyre diameter. Since 
				many of the functions of today’s vehicles are highly 
				computerized, maintaining accurate speed data going into the 
				computer assures accurate instructions coming out. And an 
				important part of the speed equation is your tyre’s overall tyre 
				diameter. For cars and vans, staying within a three per cent 
				diameter change is desirable. Pick-ups and sport utility 
				vehicles (SUVs) are usually engineered to handle up to a 15 per 
				cent oversize tyre.<br>
				<br>
				<b>Driving conditions<br>
				</b>The next important step is analysing the conditions that you 
				will be using the vehicle in be it sunshine, rain or snow, 
				though the latter is not something that we can consider here in 
				Oman but should prove useful if you plan on heading to a country 
				where it is relevant. This analysis should be taken a step 
				further when deciding on tyres for SUV’s, especially if you plan 
				on taking your vehicle off-road. <br>
				<br>
				If you live in an area where you face an extreme change in 
				weather it is advisable to have more than one set of tyres and 
				wheels (for example, summer tyres in summer and snow tyres in 
				winter). This also allows you to quite easily select tyres that 
				exactly meet your diverse needs. On the other hand if you decide 
				to use one set of tyres for every season, you may get good 
				performance under many conditions, but your vehicle’s 
				performance will be compromised when the conditions are at their 
				worst. <br>
				<br>
				When it comes to SUVs that you will be taking off road, the 
				important thing to do is to select your tyres so that they match 
				the typical off-road driving conditions you expect to encounter. 
				It is advisable to check with your local tyre dealership for the 
				best tyres to suit your conditions or even a little bit of 
				online research will go a long way in making the right choice.<br>
				<br>
				On the other hand, if you only drive around your neighbourhood 
				almost any tyre will do. But if you drive your vehicle on 
				congested city streets and expressways during rush hour you will 
				be better served by more responsive tyres. If you drive 
				extensively on high ways and consistently travel long distances, 
				you will want quiet, smooth riding, long wearing tyres. Or if 
				you like to drive quickly on twisting roads or through the 
				mountains you will want good handling tyres. And if you drive on 
				the track or in autocross events, you will want the best 
				competition tyres available. So in conclusion, if your worst 
				driving conditions and your typical conditions are similar, one 
				set of tyres will be all you need. If you live at the edge of 
				the snowbelt and infrequently get snow you may want to select an 
				all-season tyre. If your SUV is used as the family’s station 
				wagon and driven on the road all of the time, overly aggressive 
				light truck tyres aren’t for you. <br>
				<br>
				If your worst driving condition occurs frequently and is 
				dissimilar to your typical driving condition, you may want to 
				consider selecting two sets of tyres for your vehicle. Each set 
				will be designed to master the specific conditions without 
				compromising your driving satisfaction at the extremes. While 
				purchasing two sets of tyres may appear expensive, the set 
				you’re not using won’t wear while you are using the other set, 
				and combined they’ll provide longer total wear than either set 
				could individually.<br>
				<br>
				<b>Fitting to the budget<br>
				</b>And finally, the most important consideration that you will 
				have to make is deciding on a tyre that fits your budget. Buying 
				good tyres is never cheap and sometimes when you are selecting 
				new tyres and find one that is perfect, although more expensive 
				than another tyre that appears to be a close second, what can 
				you do? The answer is quite simple – consider evaluating your 
				situation by comparing ‘how much per km’ each tyre will cost. If 
				you plan to drive your vehicle another 40,000 kms and are 
				considering the ‘perfect’ tyres at 50 rials each, and the other 
				at 45 rials each; you may be surprised to find out that the cost 
				of the ‘perfect’ set costs just 1.25 baisas per km while the 
				close set costs 1.13 baisas per km. so the question you will 
				have to answer is “will saving the 20 rials today make up for 
				not having selected the perfect tyre’ that you will be driving 
				on for the next two years?</td>
