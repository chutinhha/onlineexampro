						<p class="style15">
				With summer round the corner, it is advisable to be pro-active 
				and check the condition of your tyres, lest it gets too late and 
				you’re left stranded on the road with a burst tyre. Before we 
				get down to the nitty gritties, lets understand what the 
				sidewall markings on tyres denote.<br>
				<br>
				<b>Side wall markings on a tyre<br>
				</b><br>
				<b><font color="#C00000">P215/65R15 89H</font><br>
				<font color="#008000">P: </font></b>A tyre meant for Passenger 
				Cars <br>
				<font color="#008000"><b>215:</b></font> Nominal width of tyre, 
				in this case 215mm<br>
				<font color="#008000"><b>65:</b></font> Aspect ratio = 65% of 
				215 = 139.75mm is the side wall height<br>
				<font color="#008000"><b>R: </b></font>Implies this is a radial 
				tyre, 15: Rim diameter in inches<br>
				<font color="#008000"><b>89: </b></font>Load Index, which 
				corresponds to 580kg is the load bearing capacity<br>
				<font color="#008000"><b>H:</b></font> Speed rating, corresponds 
				to a max speed of 210km/hr<br>
				<br>
				<b>Tyre maintenance<br>
				</b>Any tyre, no matter how well constructed, may fail in use as 
				a result of punctures, impact damage, improper inflation or 
				other conditions resulting from use. Tyre failures may result in 
				damage to the vehicle or personal injury. To minimise the risk 
				of tyre failure, we strongly recommend the following:<br>
				<b><br>
				Inflation pressure – </b>Tyre pressure including the spare, 
				should be checked at least once a month before driving, when the 
				tyres are cold. Maintaining the recommended tyre pressure 
				provides proper sidewall deflection and safe operating 
				temperatures for the tyres. It is also very important to tyre 
				life, safety and vehicle handling. Under inflation creates 
				excessive heat; lower load carrying capacity and can seriously 
				reduce tyre life. Irregular wear can also be caused by under or 
				over inflation. Use dust caps on valves to maintain an airtight 
				seal while keeping dust and moisture out.<br>
				<br>
				<b>Tyre rotation for passenger cars - </b>The purpose of 
				rotating tyres is to achieve a more uniform tyre wear for all 
				tyres on a vehicle. Before rotating tyres, determine the cause 
				of any unusual wear and correct any mis-alignment, imbalance or 
				other mechanical problems. First rotation should be done after 
				the 5,000 to 7,500 km and every subsequent rotation, after every 
				10,000 km. Do not mix radials and bias ply tyres on the same 
				axle. Temporary spare tyres cannot be rotated as shown above. Do 
				not mix or rotate different speed rated tyres.<br>
				<br>
				<b>Wheel alignment and balancing –</b> These are very important 
				considerations for deriving safety and maximum mileage from your 
				tyres, Check how your tyres are wearing out at least once a 
				month. If your tyres are wearing out unevenly, such as the 
				inside shoulder is wearing out faster than the rest of the 
				tread, your car may be out of alignment. This condition not only 
				reduces the life of your tyre but also adversely affects the 
				handling characteristics of your car, which could be dangerous. 
				An imbalanced tyre and rim assembly may also be a cause of 
				vibration at certain speeds. Prolonged use of the tyres in such 
				a condition can lead to tyre damage.<br>
				<br>
				<b>Tyre Replacement - </b>There are Tread Wear Indicators (TWI) 
				on the sidewall of your tyre. These indicators are at a depth of 
				1.6mm. When the tread of your tyre gets worn out due to use and 
				comes down to the level of these indicators, it is an indication 
				to replace your tyre. When your tyre gets damaged due to side 
				wall cuts, tread cuts, damaged beads etc, it is again time to 
				replace the tyre. When replacing your tyres, it is best advised 
				to stick to the specifications as laid down by the car 
				manufacturer. Filling your tyres with nitrogen would be a good 
				idea as this will keep your tyres cool and at the same time keep 
				the air pressure constant for months together.<p class="style15" style="text-align: center">
