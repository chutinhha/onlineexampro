				Ameer Mannapatt, Executive Director, Agnice International</i></font> 
				attributes his success to focused approach and unflinching 
				commitment to professionalism, a legacy he inherited from his 
				father, writes Muhammed Nafie</span></strong><font size="1"><strong><br>
				</strong><br>
				</font>
				<font face="Verdana" size="1">
				<img border="0" src="images/beyondboardrooms1-may2011.jpg" width="150" height="188" style="border: 1px solid #808080; float: right;" class="style4"></font><font size="1">I</font>t 
				requires a lot of perseverance and a more focused approach to 
				excel in an area which has nothing to do with what you have 
				studied and got trained in at college. Needless to say it will 
				be more challenging if the new pastures are a least charted 
				territory and you do not have more predecessors to guide you. 
				That was exactly the story of Suhail Ameer Manappatt, executive 
				director of Agnice Fire Protection Limited (Agnice International 
				in Oman), who proved his mettle by leading Agnice and 
				spearheading its development as a preeminent fire engineering 
				company both in India and the Middle East within a short span of 
				time. <br>
				<br>
				Manappatt was brought up in Oman and did his entire schooling in 
				Indian School Ghubra before moving to Chennai to do his 
				engineering degree in Electronics and Communication. It was 
				towards the end of the course that his father took over Agnice 
				Fire Protection in Chennai ushering in a new chapter in Suhail’s 
				life. After completing his graduation, Suhail started his career 
				in Chennai at Agnice where he was undergone thorough training on 
				the technical aspects of the business. <br>
				<br>
				“My father took over <br>
				Agnice in 2002 and I joined the company in 2005,” says Manappatt. 
				“I was there for <br>
				five years. Initially, I concentrated very well. It took about 
				one year for me to pick up and get acquainted with the new 
				field.”<br>
				<br>
				He describes how his dedication and focused approach paid 
				dividends in the end: “My father gave me a target to improve the 
				business. Since it was a niche field, we did not have enough 
				trained and experienced professionals. We trained fresh 
				graduates by giving them more money as stipend than offered by 
				other companies.” <br>
				<br>
				Varied influences<br>
				Today Agnice is one of the leading fire engineering companies in 
				India with a pan-Indian presence and offices in Mumbai, Kolkata, 
				Delhi, Bengalure d and Hyderabad. After establishing itself in 
				India, Agnice moved to Oman. Meanwhile, Manappatt did his MBA in 
				entrepreneurship from Nottingham University in UK to equip 
				himself before the company forays into the Middle East market. 
				The company started its operation in Oman with a big project-an 
				oil storage facility inside Port Sohar which has now reached the 
				final stage. He attributes his success to unflinching commitment 
				to professionalism and participative management style, a legacy 
				he inherited from his father. <br>
				<br>
				Manappatt considers his father Ameer Ahamed, chairman and 
				managing director of Manappat Group of Companies, as a role 
				model. “My father served as a great source of inspiration and 
				guidance from my childhood days. He always advised me not to 
				give priority to reputation instead of money. He used to tell me 
				that money would automatically come if you try to build your 
				reputation,” he says. Manappatt was also stimulated by his 
				maternal uncle, Mohammed Ali, Managing Director, Galfar, who 
				achieved bigger things in his life. He thinks young 
				entrepreneurs need to study and emulate the life of <i>Mohammed 
				Ali</i>. <br>
