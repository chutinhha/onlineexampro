						<font face="Verdana" size="4" color="#C00000">
						Sports, status, and the Sultanate</font></b><p class="style15">
				<strong><span class="style14">With events like the Tour of Oman 
				and the Asian Beach Games, the Sultanate has been building its 
				image as an international sport destination</span></strong><p align="justify" class="style13">
				If you are reading this column on an LG or Samsung monitor, or 
				if you drove to work today in a KIA or Hyundai, it would not 
				take much to convince you of South Korea’s successful entrance 
				into the world economy. Thirty years ago it may have been a bit 
				more difficult. The country was in the process of rebuilding 
				itself since signing a ceasefire with North Korea in 1953. By 
				the 1980s, the capital Seoul, had made miraculous gains. The 
				population had swelled from 650,000 to 10mn and economic 
				indicators marked consistent growth throughout the post-war 
				period. <br>
				<b><br>
				What does Oman of today have in common with the South Korea of 
				1980s? </b><br>
				A crucial, albeit symbolic step that lies on any country’s <br>
				path to international recognition: sports. <br>
				<br>
				Seoul won its bid to host the 1988 Summer Olympics, and as a 
				result South Korea was thrust into the international media 
				spotlight. The country occupied the centre of attention again in 
				2002 when it joined Japan in hosting the FIFA World Cup. These 
				sporting events crowned Korean economic achievements, providing 
				the invaluable albeit intangible prize of international 
				prestige. <br>
				<br>
				Lately, states in the Gulf region have been attracting an 
				increasing amount of international sporting events, and in turn, 
				gaining exposure on the world stage. When FIFA announced the 
				success of Qatar’s bid for the 2022 World Cup, the Gulf state 
				stole headlines across the globe. In recent years, both Abu 
				Dhabi and Bahrain have constructed Formula 1 tracks, attracting 
				international enthusiasts to their respective Grand Prix events.
				<br>
				<b><br>
				Niche markets</b><br>
				The Sultanate for its part seems to have its sights trained on 
				niche markets in the sports industry. One such market, cycling, 
				is experiencing increased growth. Recently, the country hosted 
				the second annual Tour of Oman, a 5-day race covering 850 km. In 
				addition to raising Oman’s profile for future cycling events, 
				the Tour should be helpful in promoting tourism. And, if 
				nurtured, a cycling tourism market could easily blossom, taking 
				advantage of the Sultanate’s dramatic views and mild winters. 
				Currently, Muscat will compete with Richmond, Virginia and 
				Quebec City, Quebec to host the 2015 UCI World Road Race 
				Championships. A successful bid for the event, which dates back 
				to 1927, would only boost Oman’s cycling credentials further.
				<br>
				<br>
				In addition to cycling, Oman is also exploring its potential in 
				golf. In a government-backed mixed-use development currently 
				under construction called The Wave, planners have been working 
				to complete the nation’s first PGA-rated course. The Wave’s 
				management is already in talks about hosting a PGA tour event at 
				the course, which was designed by golfing legend Greg Norman. 
				“Greg has designed it as such so that if we wanted to hold a 
				full PGA event, we could do so,” Michael Lenarduzzi, CEO of The 
				Wave, told Reuters. “We have had some initial discussion about 
				potentially holding a PGA event [in Oman].” A tournament such as 
				this would be a boon for both Oman’s status as a golf 
				destination and the broader economy. The government’s tourism 
				strategy focuses predominantly on expanding its offering for 
				high-spending visitors, so building a reputation as a golf 
				destination certainly seems prudent. <br>
				<br>
				The Sultanate has also been building on one of its most notable 
				natural features, its beaches. This past December, Oman hosted 
				the 2010 Asian Beach Games, a week-long competition of 42 
				nations across 14 sporting events. Omran, the government’s 
				tourism development arm, was tasked with overseeing construction 
				of the facilities for the competition. “We’ve had some small 
				projects before but this is the biggest ever,” Omran’s vice 
				president of construction, Nasser Saif al-Maqbali, said last 
				week. “The deadline was set in stone, and it was extremely 
				difficult to meet, but we were successful.” With 2,000 km of 
				largely pristine coastline and year-round sunny weather, the 
				country stands to gain from investing in facilities to 
				accommodate athletes. <br>
				<br>
				Hosting these sporting events serves two important functions: 
				building up the necessary infrastructure for hosting future 
				sports events as well as strengthening the country’s tourism 
				offering. When Barcelona hosted the 1992 Summer Olympics, the 
				city underwent a lasting transformation that boosted its economy 
				and those of surrounding regions. Visitor numbers increased in 
				subsequent years, making Barcelona one of the most-visited 
				cities in Europe. As the Sultanate promotes itself as a sports 
				destination to tourists around the world – whether they are 
				cyclists, golfers, or beach footballers – it too stands to gain 
				from increased infrastructure investments and visitor numbers.</td>
