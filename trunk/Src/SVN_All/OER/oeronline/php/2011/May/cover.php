						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The OER Top Twenty Year 2010</font></b><p class="style15">
				<span class="style14"><strong>OER presents its annual 
				presentation of Oman’s Top 20 leading listed companies for the 
				year 2010<br>
				</strong>
				</span><p align="justify" class="style13">
				year 2010 offered an interesting mix of positive and negative 
				news as investors around the world eagerly anticipated signs of 
				economic recovery and financial stabilisation. While most 
				financial markets logged positive returns for a second straight 
				year, investors had to endure a lot of bad news and pessimistic 
				market predictions. Although 2010 was a year marked by 
				uncertainty and volatility, overall it was a good year for many 
				asset markets as central bank liquidity in developed markets 
				kept investors in cautious mode for much of the year. Stocks got 
				off to a good start in January but worries about European debt 
				problems remained an underlying theme throughout the year. 
				Commodities were very bullish during the year with gold surging 
				to record highs and oil topping $90 a barrel for the first time 
				in
				two years.<br>
				<br>
				Oman’s consistent policy of economic diversification, with a 
				developing economy together with continued government spending 
				for major projects, assisted Oman in riding through this storm 
				quite successfully. <br>
				<br>
				The price of oil increased substantially during the year 2010. 
				Oman crude realised an average price of $76.6 per barrel up to 
				September 2010, compared with an average price of $56.7 for 2009 
				and the budgeted price of $50 per barrel for 2010. Due to the 
				higher oil prices than what was budgeted, it is expected that 
				most of the estimated fiscal deficit of 2010 would <br>
				be covered.<br>
				<br>
				For the year 2010, the MSM Index advanced by 6.06 per cent. This 
				is compared to a performance of 17.05 per cent in 2009. The 
				services and insurance sector was the out performer of the year 
				as the sector advanced by 0.14 per cent for the year 2010. Next 
				was the industry index which depreciated by 1.88 per cent for 
				the year. The banking and investment index closed the year with 
				a total loss of 11.25 per cent. A total of 3.02bn shares got 
				traded during the year amounting to an aggregate turnover of 
				RO1.32bn, which was down by 50 per cent compared to 2009. The 
				MSM30 index hit a low of 6,047.60 points during the year while 
				the high of the year was 6,959.03 points closing the year at 
				6,754.92. With a return of 6.06 per cent for the year, the MSM 
				has been the third best performing market in the GCC region 
				behind Qatar which advanced by 24.75 per cent and Saudi Arabia 
				which advanced by 7.8 per cent for the year. Kuwait ended the 
				year down by a marginal (0.71) per cent, Bahrain (1.78) per cent 
				and UAE (DFM) ended the year down by (9.6) per cent. <br>
				<br>
				The substantial increase in oil prices during the year was 
				supported by increased oil production and increased non-oil 
				exports which resulted in a real GDP growth rate for 2010 of 6.1 
				per cent. Oil production increased in 2010 by about 6.7 per cent 
				to 860,000 barrels per day. The government’s continued emphasis 
				on diversifying the economy away from dependence on oil gained 
				further momentum during 2010. Non oil exports grew by about 9.7 
				per cent. The government launched several major projects 
				including extension of the Muscat and Salalah airports, new 
				airports for Sohar and Ras al Hadd as well as tenders released 
				for the initial award for the design and management of a 500-km 
				railway network.<br>
				<br>
				Performance of world financial markets continued to be generally 
				positive during the year thanks in part due to aggressive 
				central bank policies. Emerging market equities had a mixed year 
				with China down by 14.31 per cent, Brazil up one per cent and 
				India up 17 per cent. MSCI AC World index was up 9.55 per cent 
				in 2010. Global equity markets finished 2010 up 26 per cent. 
				Emerging Markets with 16.36 per cent was once again the best 
				performing region followed by Asia Pacific at 14.07 per cent, 
				Japan 13.36 per cent, USA 13.18 per cent and Europe 1.02 per 
				cent. <br>
				<br>
				During the year 2010, the revenues of Oman’s 20 largest 
				companies showed an increase of RO155.96mn. Total revenues for 
				the OER Top 20 companies increased by 4.85 per cent to 
				RO3,377mn. Corporate performance of the year 2010, overall, also 
				increased. The profits for the year 2010 increased by 12 per 
				cent to RO473mn from RO423mn last year. The total market cap of 
				the OER Top 20 companies on December 31, 2010 was RO5,381mn, 
				which increased by four per cent compared to 2009. On March 31, 
				2011, the market cap of the Top 20 had gone down by 9.96 per 
				cent to RO4,860mn. The OER Top 20 companies represent 69 per 
				cent of the total market cap of the MSM of RO7,792mn at the end 
				of 2010. The average P/E ratio of the OER Top 20 based on the 
				profits of the year 2010 and the share price on March 31, 2011 
				is 9.96 times earnings. <br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover12-may2011.jpg" width="150" height="152" align="left" style="border: 1px solid #808080; padding-left: 1px">Who is out and who is in</font></b><br>
				<br>
				We have four newcomers on the list this year. Oman Qatari 
				Telecommunications Company, a newcomer in the market by virtue 
				of an IPO in 2010 comes straight in at the number nine position, 
				Al Jazeera Steel Products is back at number 15 after missing out 
				last year, Bank Sohar at number 16 and Dhofar Power at the 
				number 17 position. These companies have made it to the OER Top 
				20 this year at the expense of Oman Cement, Oman International 
				Bank, Oman Flour Mills and Sohar Power. <br>
				<br>
				The ranking of Oman’s 20 largest companies in order of revenue 
				produces a list, which includes six companies from the banking 
				and investment sector, nine from the services sector and five 
				from the industrial sector. <br>
				<br>
				<b><font size="2" color="#FF0000">Revenue differentiation</font></b><br>
				<br>
				There has not been any change in the ranking of the top five 
				companies in Oman compared to last year. Omantel continues to be 
				the largest public company in Oman with a modest growth in 
				revenue of 1.04 per cent compared to last year. Galfar 
				Engineering and BankMuscat retain their positions as No.2 and 
				No.3 despite a drop in their revenue by 9.85 per cent and 10.78 
				per cent respectively in 2010. Shell retains the No. 4 position 
				with a growth of 8.46 per cent followed by Renaissance which 
				posted a growth of 2.36 per cent. <br>
				<br>
				The chairman of Omantel HE Eng. Sultan Bin Hamdoon al Harthi in 
				his report to the shareholders explains that in spite of 
				increased competition, the retail revenue from the domestic 
				market increased by two per cent, while wholesale revenues have 
				declined by 17 per cent mainly due to opening up of the 
				international gateway business. Al Harthi states that the group 
				has achieved a net profit after tax of RO110mn compared to the 
				net profit of RO125mn in 2009. He further explains that the 
				increase in expenses is mainly due to depreciation and 
				additional expenditure resulting from the consolidation of their 
				subsidiary WorldCall. HE Al Harthi adds that the total 
				subscriber base has recorded a growth of 9.8 per cent. The total 
				number of subscribers has increased to 3.33mn compared to 3.03mn 
				last year. <br>
				<br>
				HE Al Harthi states that Oman’s budget 2011 and 8th Five Year 
				Plan projects an average GDP growth of six per cent with 
				increased government spending on infrastructure projects. This 
				would undoubtedly stimulate the growth in the telecom sector. He 
				however cautions that as the telecom sector is poised for 
				further market liberalisation, overall revenue growth will be 
				under pressure. HE Al Harthi is confident that Omantel has put 
				the right strategies in place to overcome these challenges and 
				is also working on creating sources of income and diversifying 
				the investment portfolio directed to achieve sustainable growth 
				in revenues and to maximise returns to shareholders. <br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover13-may2011.jpg" align="left" style="border: 1px solid #808080; padding-left: 1px" width="150" height="184"></font></b><font color="#FF0000" size="2"><b>Profitability index</b></font><br>
				<br>
				Four of the top five most profitable companies in Oman are the 
				same as last year with one newcomer on the list being Omani 
				Qatari Telecom at the expense of Raysut Cement. Four of the five 
				companies showed a double digit growth in profits for the year 
				whereas one showed a decline. There is only one change in the 
				top five companies by profit. Raysut Cement, which was in third 
				place last year, has been knocked out by Omani Qatari Telecom 
				which has come in straight at the number three position. 
				BankDhofar has moved up one position to number four and 
				Renaissance has slipped one position to number five. Omantel 
				continues to remain at the number one position and BankMuscat at 
				the number two spot. <br>
				<br>
				BankMuscat is the second most profitable company in Oman as well 
				as the third largest company in Oman based on turnover for the 
				year 2010. BankMuscat has retained this position from 2009, and 
				has recorded a handsome growth in profit for the year of about 
				37.82 per cent. <br>
				<br>
				The chairman HE Abdul Malik al Khalili states in his year-end 
				report to the shareholders that despite the challenging global 
				economic and financial situation, the key business lines of the 
				bank recorded healthy a performance on expected lines. <br>
				<br>
				He explains that the bank achieved a net profit of RO101.6mn as 
				against a net profit of RO73.7mn in 2009. The net profit in 2009 
				included RO53.2mn of post tax gain on the sale of HDFC Bank’s 
				investment, RO10mn losses on available for sale investments and 
				about RO60mn provision towards Saudi exposures. Excluding these 
				one off items the adjusted net profit for 2009 was RO90.5mn. 
				Thus on a like-to-like performance the bank has shown an 
				increase of 37.82 per cent over the year 2009. HE Khalili adds 
				that during 2010, the return on average assets was at 1.74 per 
				cent compared to 1.2 per cent in 2009. The return on average 
				equity was 14.6 per cent in 2010 compared to 10.9 per cent in 
				2009 and the basic earnings per share was RO0.075 as against 
				RO0.068 in 2009. HE Khalili goes on to say that the Board has 
				proposed a dividend of 40 per cent, 25 per cent in the form of 
				cash and 15 per cent in the form of stock. <br>
				<br>
				HE Khalili advises that the overall economic outlook for 2011 
				remains positive with the real GDP growth expected to gain 
				momentum due to sustained oil demand and accommodative 
				government policies. Oman’s real GDP growth is forecast to 
				accelerate further to 4.1 percent in 2011. He explains that the 
				bank has a strong reputation and is confident of retaining its 
				leading position in the country with robust policies and 
				procedures on cross border country/bank exposures to effectively 
				mitigate future challenges.<br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover14-may2011.jpg" align="left" style="border: 1px solid #808080; padding-left: 1px" width="150" height="163"></font></b><font color="#FF0000" size="2"><b>Profit growth</b></font><br>
				<br>
				Oman Holdings International has shown the highest growth in 
				profits by an enormous 470.6 per cent and comes into the number 
				one spot from number twenty last year. The top five companies 
				showing the highest growth of profit from last year are OHI, 
				Galfar, BankMuscat, BankDhofar and NBO. The last two banks being 
				new on the list. Oman Cement, Sohar Power, Al Hassan 
				Engineering, Areej Vegetable and OMINVEST have all dropped off 
				this list. <br>
				<br>
				The chairman of Oman Holdings International Maqbool Hameed Al 
				Saleh in his report to the shareholders for the year ended March 
				31, 2010 has explained that the company derives its results by 
				aggregating the results of its various subsidiaries and also 
				recognising the share of results from its associates. Unlike 
				2009, the year under review had the full year results of all its 
				subsidiaries. The subsidiaries operating in the civil 
				construction, energy and infrastructure and technology areas 
				generated net profits which were significantly higher than the 
				previous year. <br>
				<br>
				The core profits registered a growth of about 46 per cent. The 
				contribution from associate companies declined by 43 per cent. 
				Given the improved sentiments in the MSM the value of securities 
				held by the company registered an improvement of RO1.6mn which 
				helped recoup a part of the RO5.9mn unrealised loss that was 
				booked during the previous year. Al Saleh adds that a 
				combination of these factors resulted in the group generating a 
				satisfactory financial performance. <br>
				<br>
				Al Saleh is hopeful that with the improved economic sentiments 
				the valuations of securities on the MSM will improve further. He 
				also expects that the investment made by the company in the real 
				estate sector in 2007 would start yielding positive results 
				during the financial year.<br>
				The top five highest capitalised -- 2010<br>
				<br>
				Three of the top five companies that have the highest amount of 
				equity employed are banks. Four of the five of the companies in 
				this category remain the same as last year in exactly the same 
				positions. The newcomer on the list at number five is 
				Renaissance Services which has replaced Oman International Bank.
				<br>
				<br>
				The chairman of NBO Omar al Fardan in his report to the 
				shareholders states that the bank achieved a net profit of 
				RO27.2mn for the year compared to RO21.1mn for 2009, an increase 
				of 29 per cent. Al Fardan remarks that the net spreads improved 
				to 3.28 per cent in 2010, up from 3.24 per cent in 2009. 
				Nonperforming loans decreased to 3.5 per cent in 2010 from 4.3 
				per cent in 2009. Al Fardan adds that the board has recommended 
				a cash dividend of RO0.015 per share this year based on the 
				dividend policy approved by the board of directors. <br>
				<br>
				Al Fardan is optimistic that global financial stability in the 
				markets will be sustained in 2011 resulting in reasonable 
				economic growth in the years ahead. The bank will continue to 
				focus on leveraging its investment in its retail banking 
				franchise, the credit cards business and the SME sector. The 
				wholesale and investment banking franchise of the bank will 
				continue to support domestic project financing and all related 
				activity. <br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover15-may2011.jpg" align="left" style="border: 1px solid #808080; padding-left: 1px" width="150" height="180">Market capitalisation</font></b><br>
				<br>
				This year again, two of the top five companies that have the 
				highest market capitalisation on the MSM are not banks. 
				BankMuscat has overtaken Omantel to rank number one on the MSM 
				in terms of market cap. Omantel has slipped to the number two 
				slot in this category. BankDhofar has moved up one place to the 
				number three spot. Oman Qatari Telecom is the newcomer straight 
				into the number four slot and NBO that was number three last 
				year has slipped to the number five spot. OIB that was number 
				five on this list last year has moved out. <br>
				<br>
				The chairman of BankDhofar, Eng Abdul Hafidh al Aujaili in his 
				annual report to the shareholders states that the bank stood 
				firm on fundamentals with strict adherence to prudent risk 
				management practices and a clear strategy plan. This approach 
				has enabled the bank to witness appreciable growth in most of 
				the financial indicators in the year 2010. Al Aujaili explains 
				that the bank achieved a net profit of RO33.2mn for the year 
				2010 compared to RO25.4mn for 2009, an increase of 31.1 per 
				cent. In the light of these results the board of directors has 
				proposed a cash dividend of 12.5 per cent and a bonus share 
				issue of 12.5 per cent. <br>
				<br>
				Al Aujaili states that the implementation of the new core 
				banking solution was successfully completed in the month of July 
				2010 resulting in improvement of the banking services from all 
				delivery channels. This new customer friendly solution would 
				enable the bank to adopt advanced banking practices and to be 
				more competitive in terms of new products and services besides 
				instituting better monitoring controls. <br>
				<br>
				<font color="#FF0000" size="2"><b>Returns on equity</b></font><br>
				<br>
				Interestingly, none of the top five companies showing the best 
				return on equity employed come from the banking sector. Three of 
				the companies in the top five are newcomers to the list. Shell 
				remains at the number one spot as last year. Oman Qatari Telecom 
				a newcomer on the list comes straight in at the number two spot 
				while Oman Oil Marketing, another new entrant, comes in at the 
				number three spot. Omantel has dropped two places to the number 
				four spot and OHI is the third newcomer on this list. OMINVEST, 
				Raysut Cement and Areej Vegetable are knocked out of this list.
				<br>
				<br>
				John Blascos, chairman of Shell Oman Marketing, in his report to 
				the shareholders states that after the unsettled economic 
				scenario witnessed in the region in 2008 and 2009 the company 
				posted stable and strong results in 2010. During the year the 
				company continued to focus on health, safety, security and 
				environment as a top priority. The company made major efforts to 
				further upgrade customer service in all the retail service 
				stations and continued to deliver exceptional basics, quality 
				fuels and operational excellence. Blascos explains that the 
				retail business remains the most important segment for the 
				company. The aviation business continues to be highly 
				competitive and the company continued its presence at both the 
				major airports in the country in addition to customers’ owned 
				fields.<br>
				<br>
				The market in lubricants also achieved higher penetration. <br>
				<br>
				Blascos adds that the company achieved strong financial results 
				with a record net profit of RO14.0mn compared to previous year’s 
				net profit of RO13.0mn. Despite the uncertainty surrounding the 
				major economies, proactive management as well as sound 
				operational performance were the principal drivers of the 
				commendable financial performance in 2010. The board recommended 
				a final dividend of 130 per cent for the year continuing the 
				recent trend of year on year dividend growth. <br>
				<br>
				Blascos adds that the fuels market normally follows the economic 
				trend of the country and is thus expected to be stable, growing 
				in tandem with economic activities. New development in road 
				network and residential area will open up opportunities for 
				addition of new retail sites. Major infrastructure projects 
				announced by the government will see some activities in 2011, 
				and the company will ensure that it is ready to handle stiff 
				competition to get its share of the new business by investing in 
				new systems and business redesign. <br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover16-may2011.jpg" align="left" style="border: 1px solid #808080; padding-left: 1px" width="150" height="211"></font></b><font color="#FF0000" size="2"><b>Earnings per share growth</b></font><br>
				<br>
				All of the top five earnings per share growth companies are 
				newcomers on this list. Oman Holdings International jumps 
				straight to the number one spot. Al Jazeera Steel comes in at 
				the number two position, Galfar at number three, BankDhofar at 
				number four and NBO at number five. Oman Cement, OMINVEST, Al 
				Hassan, Areej Vegetable and Oman Cables who were on this list 
				last year have all disappeared from this list. <br>
				<br>
				Salim Said Hamad al Fannah Al Araimi, chairman of Galfar, in his 
				report to the shareholders states that the company continued to 
				be a preferred contractor in the Sultanate of Oman for many 
				clients. During 2010, the company bagged RO380mn worth of 
				projects to maintain the order book back log at RO614mn. Al 
				Araimi adds that infrastructure projects in India are continuing 
				at a rapid pace with the major thrust presently in roads and 
				ports. <br>
				<br>
				He explains that Galfar Kuwait and Galfar-Wazen Libya are new 
				additions to the group along with Galfar India. Other 
				subsidiaries like Galfar Training Institute and Al Khalij 
				Equipment &amp; Engineering also continue to do well. Al Araimi adds 
				that with numerous projects both in the oil and gas and other 
				infrastructure development sectors already in the tendering 
				stage, he expects that Galfar will consolidate and register a 
				growth in 2011 and the future. <br>
				<br>
				<b><font size="2" color="#FF0000">
				<img border="0" src="images/cover17-may2011.jpg" align="left" style="border: 1px solid #808080; padding-left: 1px" width="150" height="176"></font></b><font color="#FF0000" size="2"><b>Share price growth</b></font><br>
				<br>
				Four of the five companies on this list are newcomers. Al Hassan 
				has dropped to number three from the number one position last 
				year. Renaissance comes in straight into the number one position 
				with a share price growth of 44.84 per cent, followed by Oman 
				Oil Marketing in the second position. BankMuscat comes in at 
				number four and NBO at number five. Oman Flour, Oman Cement, 
				BankDhofar and Oman Cables who were on the list last year have 
				all dropped out. <br>
				<br>
				Samir Fancy chairman of Renaissance in his report to the 
				shareholders states that the company is reporting record results 
				for the 10th consecutive year in spite of the enduring effects 
				of the global economic recession. He adds that the company is 
				also presenting a bold initiative for the approval of the 
				shareholders to publicly list the Marine &amp; Engineering business 
				on the London Stock Exchange. This move would bring 
				transformational change to the company ensuring potential for 
				growth, access to capital and value recognition. Fancy explains 
				that the 2010 results demonstrate again the resilience of the 
				business model and the ability to perform under the most 
