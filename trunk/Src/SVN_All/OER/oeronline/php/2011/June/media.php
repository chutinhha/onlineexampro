						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>The perfect Alchemy<br>
&nbsp;</strong></span><p class="style15">
				<img border="0" src="images/media1.jpg" style="border: 1px solid #808080" align="left"><p class="style15">
				<strong><span class="style14">Merge 104.8 FM is looking at 
				redefining listening habits and growing the radio market in the 
				Sultanate. Mayank Singh reports<br>
				</span></strong><p align="justify" class="style13">
						<p align="justify" class="style13">
				Well begun is half done, they say. Going by the adage, Merge 
				104.8 (Merge), the upcoming English language FM radio station 
				from SABCO Media seems to have hit the right note. The station 
				which goes live on June 7, 2011 won two silver awards for its 
				unique corporate identity and stationery design at the annual 
				Lynx Awards in Dubai recently. Merge is the first radio station 
				to enter the market after a hiatus of over three years. Al Wisal, 
				SABCO’s Arabic station was the last station to be launched in 
				March 2008.<br>
				<br>
				The name Merge is inspired by Oman -- a country which is a 
				crucible of various cultures, nationalities and influences and 
				the name was seen as a fitting representation of what the 
				station stands for. Says Eihab A. Abutaha, general manager, 
				SABCO Media, “We looked at the market and felt that there was a 
				huge gap between listener expectations and what was on offer. 
				Second, we have identified our target audience and are confident 
				that if we offer something that is relevant to this audience 
				they will surely tune into Merge.” The station’s tag line – 
				‘Global flavour local spice’, reinforces the confluence of 
				cultures theme. In terms of its primary audience, the station is 
				targeting English-speaking Omanis, Asian and Arab expatriates.
						<p align="justify" class="style13">
				<br>
				<br>
				<img border="0" src="images/media2.jpg" width="150" height="150" style="border: 1px solid #808080" align="left"><b><font color="#008000" size="2">Strong tailwinds</font></b><br>
				<br>
				SABCO’s experience with Al Wisal gives Merge a head start in 
				terms of product knowledge and market dynamics. “The biggest 
				learning from Al Wisal has been insights about being closer to 
				society,” says Abutaha. The new station is banking on its 
				programming mix to appeal to its target audience. Nadim Attieh, 
				station manager, Merge says, “Our effort is to encourage 
				listeners to tune in for a longer duration, like one and a half 
				hours a day and for this the programming has to be tight.” <br>
				<br>
				The onus of delivering this largely rests on Chris Fisher, the 
				station’s presenter cum programme director and a veteran who has 
				spent 18 years in the radio industry. Fisher says that market 
				research revealed major lacunae in the radio industry in the 
				Sultanate – there were a few stations which were doing 
				everything, but in an unstructured fashion, others were 
				concentrating on local mores but with an over-the-top approach 
				while a few were catering only to a niche audience.<br>
				<br>
				Merge’s programming grid tries to strike a balance between 
				generic and specialist shows. The generic programmes will be 
				played out during the day and early evenings while specialist 
				shows will be aired after 10pm. Weekends will see special shows 
				like Oman Talents, Middle East Top 30, Weekend Drive etc. The 
				station has lined up a mix of local and international RJs to 
				appeal to both traditional and contemporary cultures. <br>
				<br>
				SABCO is pulling out all the stops to ensure the success of 
				Merge. A strong advertising campaign using outdoors and print 
				has been planned. The campaign will encourage listeners to 
				‘Switch to 104.8 FM.’ A professional agency has been hired to 
				disseminate information about the station using social media 
				like Facebook, Twitter and web streaming. The station has 
				invested in state-of-the- art technology and towers. Says 
				Abutaha, “We have looked at every touch point, the demographics 
				and psychographics of Omanis, expatriates, the aspirations of 
				different age groups and based on all this feedback we came up 
				with a station that is based on what people want us to be.” And 
				SABCO promises to continue the learning process. “We are not 
				only a radio station to listen to but also a station which 
				listens to its audience,” says Attieh. <br>
				<br>
				Merge will be the fourth private radio station to enter the 
				market after Hi, Hala and Al Wisal. The Sultanate presently has 
				seven radio stations. For listeners a new station offers more 
				choices and is sure to be a welcome addition. The move is also 
				expected to strengthen the radio industry in Oman but the 
				million dollar question is whether advertisers will bite the 
				bullet? In a market dominated by newspapers, radio remains a 
				growing but small player in the advertising sweepstakes.
						<p align="justify" class="style13">
				<br>
				<br>
				<img border="0" src="images/media3.jpg" width="150" height="151" style="border: 1px solid #808080" align="left"><font color="#008000" size="2"><b>Bang for the buck</b></font><br>
				<br>
				Feroz Khan, director of sales and marketing, Merge says, 
				“Clients look for reach and return on investment and as travel 
				time increases the reach of radio can be seen and felt clearly. 
				Secondly, radio advertisers can focus on their target group 
				(TG).” He goes on to state that if an advertiser is looking at 
				selling a product to women, he can easily place his spots during 
				those hours of the day when more ladies are listening to the 
				station. Similarly, for someone looking at reaching out to Omani 
				youth, concepts like broadcasting out of a University can be 
				created helping advertisers to connect with their TG. “Newspaper 
				advertising is a case of hit and trial, but radio is passive, 
				interactive and engaging and this helps in reaching out to ones 
				audience,” adds Abutaha. <br>
				<br>
				Votaries also argue that radio lends itself naturally to 
				tactical advertising like sales, shopping festivals etc. Abutaha 
				does a back of the envelope calculation to show that radio 
				advertising works out to be cheaper than advertising in 
				newspapers. A quarter page advertisement in a leading newspaper 
				would cost upwards of RO1,000 and an advertiser taking two 
				insertions has a 20 second chance of being seen by the intended 
				audience. The same RO2,000 spent on radio translates into 40-50 
				spots played over a 10-15 day period. A 30 second spot on radio 
				costs anywhere between RO30-150 based on when it is being 
				played. With close to 80 per cent of businesses in Oman being 
				small and medium enterprises (SMEs), a RO50,000-60,000 newspaper 
				campaign is clearly out of their reach, but radio is something 
				that suits their pockets. Says Khan, “Advertisers needed choices 
				as far as English radio stations are concerned and they are 
				excited about Merge.”<br>
				<br>
				“We are all passionate about making Merge a success. So while 
				profitability is important, money is going to be a by- product 
				of engaging our audience and giving advertisers a bridge to 
				reach out to them,” sums up Abutaha. Having started on a winning 
				wicket, Merge is looking at keeping up the momentum. Well, we do 
				not have to wait for long to test the station’s promise.<br>
				<br>
