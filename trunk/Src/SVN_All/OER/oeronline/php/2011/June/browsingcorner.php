						Getting rid of ineptitude</font></b><p align="justify" class="style3">
				<span class="style5"><strong>Atul Gawande’s The Checklist 
				manifesto: How to get things right examines how effectively 
				checklists can hold in check avoidable failures which are common 
				and persistent in all walks of life from medicine to finance, 
				business to government. <i>A review by Muhammed Nafie</i><br>
				<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/browsing1jpg.jpg" style="border-style:solid; border-color:#808080; float: right" class="style4" width="238" height="309"></font>Ours 
				is a time when humanity is saved by knowledge as much as it is 
				burdened with it. The volume and complexity of human knowledge 
				has become so phenomenal that it far exceeds our ability to 
				deliver correctly, safely and reliably. Though we have 
				accumulated stupendous know-how and put it in the hands of the 
				most highly talented, trained, and hardworking people, avoidable 
				failures are common and persistent in all walks of our life from 
				medicine to finance and business to government. Despite the 
				dazzling success of medical science and the spectacular 
				improvement in surgical capability, major errors are being 
				repeated even by the super specialists. It points to some 
				inevitable human inadequacies which may creep into whatever we 
				do making a mockery of our abilities and expertise.<br>
				<br>
				The Checklist manifesto: How to get things right by Atul Gawande, 
				internationally acclaimed writer and surgeon, suggests a 
				different strategy for overcoming these failures which are 
				frequented even by the best-trained people. The book explains 
				how checklists serve as a solution to build on and take 
				advantage of humanity’s collective knowledge and expertise, 
				however ridiculous the idea may sound initially.<br>
				<br>
				Written in a luminous prose lifting effortlessly off the page, 
				the book examines how checklists can bring about striking 
				improvements everywhere from medicine to investment banking, 
				cooking and professions and businesses of all kind. <br>
				<br>
				Some of the most difficult feats such as building skyscrapers of 
				mindboggling sophistication and flying planes were made possible 
				by checklists. In the complex world of surgery, a 90-second 
				checklist which could reduce deaths and complication by more 
				than one-third in hospitals around the world at virtually no 
				cost is being adopted throughout the NHS and healthcare services 
				worldwide.<br>
				<br>
				Though even experienced personnel fail to grasp the importance 
				of certain precautions, checklists helps them recall and clearly 
				set out the minimum necessary steps in a process and establish a 
				higher standard of baseline performance.<br>
				<br>
				Apart from giving a detailed account of some successful 
				checklist stories, Gawande also describes how he prepared a 
				surgery checklist and set a research team to work making it more 
				usable. While drafting the checklist, they adopted a do-confirm 
				rather than a read-do format to give people greater flexibility 
				in performing their tasks. It also made them stop at key points 
				to confirm that critical steps have not been overlooked. After 
				three months of using the checklist, almost 78 per cent of 
				surgeons, anesthesiologists, nurses and others found it very 
				easy to use and observed that it had improved safety of care and 
				prevented errors in the operating theatre. However, he complains 
				that the doctors are a long way from actually embracing the 
				idea.<br>
				<br>
				Checklist promote a culture of teamwork and discipline and make 
				sure that we apply the best of knowledge and expertise we have 
				consistently and correctly. When half of heart attack patients 
				do not get their treatment on time because of errors, and not 
				because of scarcity of knowledge or expertise, Checklist emerge 
				as an important solution to save us collectively from being 
				accused of ineptitude and recklessness.<br>
