						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						GCC economic integration: <br>
						A long way to go<br>
&nbsp;</font></b><p align="justify" class="style13">
				<strong><span class="style14">In the 13th Gulf Cooperation 
				Council (GCC) consultative summit GCC officials feel embarrassed 
				about the lack of progress on integration many years after it 
				came into effect</span></strong><p align="center" class="style13">
				<strong><span class="style14"><br>
				</span></strong><br>
				<img border="0" src="images/closeup.jpg" style="border: 1px solid #FFFFFF; "><br>
&nbsp;<p align="justify" class="style13">
				The 13th Gulf Cooperation Council (GCC) consultative summit held 
				in Riyadh paid only lip service to economic issues facing the 
				six-nation group. The leaders used the meeting to discuss 
				emerging political and security matters engulfing the region, 
				notably developments in Bahrain and Yemen. Scio-political 
				problems have lately become the norm rather than exception in 
				the larger Middle East and North Africa (MENA) region. 
				Undoubtedly, GCC countries cannot overlook possible adverse 
				effects of what happens in Libya and Syria on their security 
				arrangement. <br>
				<br>
				<b><font color="#008000" size="2">Customs union</font></b><br>
				<br>
				Nevertheless, the final communique touched on the need to 
				resolve outstanding issues related to implementation of the 
				customs union project. <br>
				<br>
				For obvious reasons, GCC officials feel embarrassed about the 
				lack of progress on this integration project many years after it 
				came into effect. Conversely, real progress has occurred in 
				subsequent integration schemes, notably the Gulf Common Market (GCM), 
				which started in 2008.<br>
				<br>
				In retrospect, the customs union project went into effect in 
				2003 with the original plan calling for its full implementation 
				in a span of two years. Yet, the GCC’s 26th summit held in Abu 
				Dhabi in late 2005 pushed the deadline back by two more years, 
				but that didn’t help much. No fresh deadlines were placed until 
				very recently or in the run-up to the 13th consultative summit.<br>
				<br>
				Unlike earlier deadlines, the new one seems realistic rather 
				than idealistic. The new policy calls for developing a plan by 
				this year-end to resolve all remaining issues facing the customs 
				union project over the next three years, so as to ensure its 
				full implementation at the start of 2015.<br>
				<br>
				<b><font color="#008000" size="2">Overcoming obstacles</font></b><br>
				<br>
				The new approach was adopted during meeting No. 90 of the GCC’s 
				finance ministers that discussed a range of economic issues of 
				collective concern. The meeting was held in Abu Dhabi with the 
				UAE heading the GCC until the next regular summit in Saudi 
				Arabia towards the end of this year.<br>
				<br>
				To be sure, the customs union encountered serious challenges 
				ever since its inception, notably the need to develop a formula 
				to ensure fair distribution of customs duties acceptable to all 
				six states. Sticking points include port of entry and final 
				destination of goods<br>
				Another issue requiring concrete solution is ensuring smooth 
				movement of trucks carrying goods at crossing points. This is 
				particularly the case on King Fahd causeway linking Bahrain to 
				Saudi Arabia as well as border crossing points of Saudi Arabia 
				and the UAE. Suggested solutions call for ensuring availability 
				of visas for non-GCC drivers around the clock on the one hand 
				and sufficient support at entry points.<br>
				<br>
				<b><font color="#008000" size="2">Common market</font></b><br>
				<br>
				As mentioned earlier, GCC states found greater success with the 
				GCM scheme despite it coming into effect some five years after 
				the customs union project. The GCM allows free movement of 
				factors of production among member states. The project covers 
				all economic and investment services, dealings in the stock 
				market and setting up of companies in the public and private 
				sectors besides social insurance among GCC citizens. <br>
				<br>
				Still, the last two GCC summits added new dim-ensions to the GCM, 
				with the 30th summit held in Kuwait in 2009 adding an extra item 
				to the list, namely access to technical studies provided in 
				member states. Also, the last summit held in the UAE saw an 
				agreement to allow GCC firms the right to open branches in 
				member states and to be treated as local ones.<br>
&nbsp;<br>
				Still, the last two GCC summits added new dimensions to GCM, 
				with the 30th summit held in Kuwait in 2009 adding an extra item 
				to the list- access to technical studies provided in member 
				states. The last summit held in the UAE saw an agreement on 
				allowing GCC firms the right to open branches in member states 
				and to be treated as local ones.<br>
				<br>
				Not surprisingly, the GCM project seems popular with the 
				business community for the intended purpose of granting 
				merchants unrestricted access to regional economies. Besides, 
				the scheme provides people in the GCC with an opportunity to 
				realise the benefits of the regional grouping.<br>
				<br>
				<b><font color="#008000" size="2">Monetary union</font></b><br>
				<br>
				Concurrently, the 13th consultative summit offered few clues 
				about the progress of the Gulf Monetary Union (GMU) project, in 
				turn officially launched at the start of 2010 but with no 
				specific deadline. Only Saudi Arabia, Qatar, Kuwait and Bahrain 
				are parties to the venture. The GMU’s final goals include 
				unifying monetary and fiscal policies and therefore introducing 
				a single currency for member countries Certainly, GCC people 
				cannot be blamed for looking for economic benefits from the 
				grouping that came into being more than 30 years ago.<br>
				<br>
				Undoubtedly, delays in the comprehensive implementation of the 
				customs union project only undermine popular appreciation of 
				integrating projects altogether. Other than the customs union, 
				hopes remain high for the GCM, which is designed to remove all 
				sorts of restrictions on intra-GCC business by promoting 
				economic citizenship. <br>
