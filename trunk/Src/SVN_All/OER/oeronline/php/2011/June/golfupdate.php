				</span></strong><p align="justify" class="style13">
				Severiano ‘Seve’ Ballesteros Sota was a Spanish professional 
				golfer, a World No. 1 who was one of the sport’s leading figures 
				from the mid-1970s to the mid-1990s. Ballesteros was a five-time 
				major champion whose incomparable imagination and fiery 
				personality made him one of the most significant figures in 
				modern golf. <br>
				<br>
				He gained attention in the golfing world in 1976, when at the 
				age of 19 he finished second at The Open Championship (British 
				Open). A member of a gifted golfing family, Ballesteros won five 
				major championships between 1979 and 1988, the Open Championship 
				three times, and the Masters Tournament twice. He was also 
				successful in the Ryder Cup, helping the European team to five 
				wins both as a player and captain, and won the World Match Play 
				Championship a record-tying five times. He is best known for his 
				great short game, and his erratic driving of the golf ball. He 
				is generally regarded as the greatest Spanish golfer of all 
				time.<br>
				<br>
				Because of back-related injuries, Ballesteros struggled with 
				form during the 1990s. In spite of this, he continued to be 
				involved in the game of golf, creating the Seve Trophy and 
				running a golf course design business. Ballesteros eventually 
				retired from competitive golf in 2007 because of continued poor 
				form, and in 2008 was diagnosed with a malignant brain tumour. 
				After reports that Ballesteros would make a return to the 
				spotlight at the 2010 Open Championship, following the advice of 
				doctors he did not travel to St Andrews in 2010 for the Open 
				Championship.<br>
				<br>
