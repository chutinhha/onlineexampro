							<font size="2" color="#008000">
							<img border="0" src="images/cover-6.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="147"></font></b><font color="#006600" face="Verdana" size="1">Al 
							Bustan Palace Hotel believes that it has the 
							responsibility to respect its environment and the 
							communities where it operates. The hotel is working 
							hard to ensure that it makes a positive difference 
							by focusing on four key action areas, namely energy, 
							waste, water and community.<br>
							<br>
							“The Al Bustan Palace InterContinental Muscat 
							recognises that the link between tourism and climate 
							change has led to pressure on the hotel industry to 
							be more concerned about their environmental impact 
							and carbon footprint. Therefore, we are keen to find 
							new ways to encourage fellow colleagues and guests 
							to recycle,” says Martin van Kan, general manager of 
							Al Bustan Palace InterContinental Muscat, and 
							director of operations for InterContinental Hotels 
							Group (IHG) – Oman.<br>
							<br>
							Out of the many initiatives undertaken over the past 
							25 years, the Al Bustan family is particularly proud 
							of the Al Bustan Palace sewage water treatment 
