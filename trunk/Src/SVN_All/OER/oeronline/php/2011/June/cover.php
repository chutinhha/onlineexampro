						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						People Planet Profit</font></b><p class="style15">
				<span class="style14"><strong>Switching off lights, planting 
				trees, beach cleaning and numerous other activities are all 
				fine. However, Oman has a lot to do on two crucial fronts – 
				wastewater and solid waste – to preserve Oman’s environment on a 
				sustainable basis. Visvas Paul D Karra digs deeper into this 
				urgent issue<br>
				</strong>
				</span><p align="justify" class="style13">
				The rise of a strong industry capable of performing its national 
				role in social progress and prosperity, while at the same time 
				maintaining the purity and cleanliness of the environment at the 
				highest possible level will always be our strategic goal. We 
				call upon all officials and others involved in industry to 
				achieve this aim.”<br>
				<br>
				An excerpt from the speech of His Majesty Sultan Qaboos bin Said 
				on the occasion of the 21st National Day (18.11.1991).<br>
				<br>
				Most of the pro-environment initiatives in the country stem from 
				this precise directive of His Majesty The Sultan who is a 
				staunch votary of preserving the environment. Oman is one of the 
				few countries that has placed environment at heart of the matter 
				a long time ago and one of the first countries in the region to 
				create an environment ministry and formulate environmental laws 
				for the protection of the environment in terms of its flora and 
				fauna.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-1.jpg" width="130" height="147" align="left" style="border: 1px solid #C0C0C0">A 
				pat is not enough</font><font size="2" color="#000080"><br>
				</font></b><br>
				Many private sector organisations have made the concept of 
				‘going green’ an integral part of their CSR (corporate social 
				responsibility) policy. However, environmental experts warn that 
				giving ourselves a pat on the back over the ubiquitous 
				environmental activities is clearly not enough. Underneath the 
				green veneer, the surface is still raw and it would be a 
				misnomer to dub such efforts as a movement. Yes, things are 
				moving, says <i>Omar Khalfan Nasser Al Wahaibi</i>, CEO, Haya 
				Water, but we are at the beginning of the road and we need to 
				put more resources to carry it forward in a sustainable manner. 
				“The two most pressing issues that are at the base of 
				environment protection in Oman are wastewater and solid waste 
				management. This has to be driven by environmental agencies with 
				the ministry or environment supporting the other authorities to 
				achieve these goals,” Wahaibi says.<br>
				<br>
				Consider these numbers: Haya Water for the greater capital area 
				of Muscat and the Salalah Sanitary Drainage Company in Salalah 
				are the only two agencies that are working towards wastewater 
				treatment. But in the rest of Oman, there is plenty of untreated 
				wastewater flowing around.<br>
				<br>
				The Census estimates shows that there are 1.6 million people 
				living outside Muscat and Salalah, but the total wastewater 
				network coverage in Oman is less than 10 per cent. It means that 
				all the wastewater produced by the rest of the population is 
				polluting the environment. <br>
				<br>
				In terms of solid waste management – the household garbage, 
				medical waste and industrial waste etc – we have just one 
				engineered landfill in the whole of Oman. Whereas, the Municipal 
				Solid Waste (MSW) generated by households throughout Oman is 
				estimated at 1.6 million tonnes per year. Actually, each 
				individual generates an average of 1.6 kilo of waste on a daily 
				basis.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-5.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="151">Main 
				challenge</font></b><br>
				<br>
				<i>Said Darwish Al Alawi</i>, director general of Health Control 
				and Waste Water, Ministry of Regional Municipalities &amp; Water 
				Resources, admits that the main challenge across the country is 
				the disposal of the waste both solid waste as well as waste 
				water.<br>
				<br>
				If you are talking about solid waste management in general, 
				everyone feels satisfied about the cleanliness of the country, 
				but the problem of final disposal still persists. And for this, 
				the Oman Environmental Services Holding Company (OESHCO) is 
				drawing up a master plan, says Alawi. There are 44 
				municipalities, which come under the purview of the ministry of 
				regional municipalities with large and small towns and villages.
				<br>
				<br>
				In all these 44 municipalities, there are only two sanitary 
				landfills and more than 200 dumping sites. A consultant has been 
				appointed to study and identify the locations for the new 
				landfills and new transfer stations. “Presently, the 
				municipality in each city or town collects the waste and buries 
				it as open burning is not allowed. There is proper equipment in 
				different sites to take care of the final disposal but 
				unfortunately, we don’t have that much of recycling and 
				therefore we are encouraging the private sector to take up the 
				recycling,” Alawi points out.<br>
				<br>
				The other challenge is to manage the dumping sites and the 
				government has put this on the priority list. The same is the 
				case with wastewater. Most the cities have STPs (sewage 
				treatment plants) but the coverage of network is insufficient 
				when compared to the population.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-4.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="153">Cash 
				from trash</font></b><br>
				<br>
				There is a huge amount of economical and environmental benefits 
				from managing the waste (both wastewater and solid waste). 
				“Right now, all the solid waste that is going into the landfills 
				or dumping sites around the country is actually a monumental 
				waste of money. More than 80 per cent of all that waste can be 
				recycled,” says Dr Puneet Sarna, business development manager, 
				Al Ahlia Environmental Services Co, which is managing industrial 
				waste generated in the Sohar Port and Sohar Industrial Area. <br>
				<br>
				Any waste that is generated by manufacturing industries is 
				considered hazardous waste. In general, terms, hazardous waste 
				contains heavy metals and toxic organic compounds. And there are 
				about 70 parameters to test any industrial waste and categorise 
				it as waste. As a first of its kind project in the country, the 
				Al Batinah Hazardous Waste Storage facility being managed by Al 
				Ahlia became operational in April 2010 and presently it is only 
				used to store the waste. There are about 15 companies identified 
				in Sohar, which generate hazardous waste.<br>
				<br>
				Al Ahlia is presently in the process of bidding for a number of 
				projects announced by OESHCO for managing all kinds of wastes. 
				These include the National Hazardous Waste Management Facility 
				in Adam; Muscat Solid Waste Treatment Plant (two sites); a 
				bio-medical waste management facility and a few more projects 
				all related to waste.<br>
				<br>
				Wahaibi says that by taking the wastewater for treatment, we are 
				protecting the underground aquifers from pollution. And by 
				treating the water and making that water reusable, we are saving 
				on desalination water, which translates into energy and by 
				burning less hydrocarbons we are reducing the amount of 
				greenhouse emissions. “We are also taking sludge and treating it 
				into compost, which goes back to the environment in the form of 
				a natural fertiliser instead of sitting in a landfill and 
				creating more greenhouse gases,” informs Wahaibi.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-3.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="136">Cradle 
				to cradle</font></b><br>
				<br>
				The OESHCO has come up with a novel ‘Cradle-to-cradle’ concept 
				for sustainability in waste management, in an endeavour to make 
				Oman a zero waste country. Waste becomes resource in the 
				cradle-to-cradle concept in which all waste elements end up in 
				new products (See diagram).<br>
				<br>
				OESHCO was established in July 2007 based on the recommendations 
				of the National Solid Waste Management Strategy Report 
				commissioned by the Ministry of National Economy to streamline 
				and privatise waste management and environmental protection 
				activities across the Sultanate. “There are about 348 dump sites 
				or landfill sites ranging from large sites serving a community 
				of 100,000 to those serving less than 500 in some cases. These 
				landfill sites if not properly managed could pose numerous 
				health and environment hazards,” discloses Lt Col (Rtd) Azhar 
				Haroun Al Kindi, CEO, OESHCO One of the main activities of 
				OESHCO in the next two years will be to re-align the waste 
				management strategy. This will provide the blueprint for the 
				restructuring and privatisation of the waste management sector 
				in Oman. OESHCO is working closely with all the regional 
				municipalities and other key organisations to gradually take 
				over the waste management responsibilities from individual 
				municipalities and privatise the waste management sector.<br>
				<br>
				<b>Presently, OESHCO is busy implementing the following 
				projects: <br>
				</b><br>
				Fast Track Projects as identified in the National Solid Waste 
				Management Draft Strategy Report<br>
				<br>
				Waste Sector Emergency Project – design, construction and 
				management of 16 engineered landfills, 65 waste transfer 
				stations and four waste treatment plants<br>
				<br>
				<b>Quick Wins Projects as follows:<br>
				</b><br>
				Management of used tryes, electronic waste and healthcare waste<br>
				Construction &amp; Demolition Waste – recycling plan to manage C&amp;D 
				waste <br>
				Management of paper recycling <br>
				Closure of dumpsite in Muscat <br>
				Development of Waste Information System (WIS)<br>
				Waste Management Services –Proposal for pilot project for 
				collection, transportation and disposal in Al Buraimi and Al 
				Sharqiya regions<br>
				<br>
				As part of its corporate vision to move Oman towards zero waste 
				by providing safe, efficient and most economical environmental 
				services in innovative ways, OESHCO is also laying stress on 
				raising the awareness levels in the community by promoting the 
				concept of reduce, reuse and recycle of solid waste. 
				Individually and collectively, citizens are capable of doing 
				more than they thought possible.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-7.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="138">Sustainability 
				is the key</font></b><br>
				<br>
				For many private sector companies, the definition of environment 
				preservation and protection is directly correlated to 
				sustainability. Many private sector industries have realised 
				that they need to balance between socio-economic development in 
				the areas where they operate and maintain high-quality natural 
				resources, biodiversity and life.<br>
				<br>
				Says Sergio Leite, country manager, Vale Oman, “Our preservation 
				activities revolve around understanding the local environmental 
				needs while preventing and minimising the negative impacts and 
				risk of our operations on the ecosystem. Furthermore, we strive 
				to continuously invest, improve and develop technologies that 
				will restore degraded areas and encourage biodiversity 
				protection.”<br>
				<br>
				Vale has invested approximately $40mn in its Pelletising Plant 
				Industrial Complex in Sohar for the installation of advanced 
				pollution control and environmental safety systems designed to 
				control dust emissions, noise, solid residues and effluent 
				quality. The first innovative approach towards controlling iron 
				ore dust is the installation of 24 meter high wind fences 
				surrounding the stockyards, which have the ability to trap the 
				smallest of particles. Studies show that wind fences cut 
				emission of dust by 77.4 per cent.<br>
				<br>
				In addition, Vale is planting two to 20 metre high trees in an 
				oval formation, around the plant. The trees will not only add an 
				aesthetic element to the landscape but will also act as a 
				natural barrier to dust. To further enhance control of dust 
				emissions, the iron ore pellets are coated with a gelatine like 
				substance and sprayed with water to ensure that dust remains 
				intact.<br>
				<br>
				<b><font size="2" color="#008000">
				<img border="0" src="images/cover-2.jpg" align="left" style="border: 1px solid #C0C0C0" width="130" height="142">Corporate 
				social responsibility</font></b><br>
				<br>
				“Sustainable development must increase the quality of life and 
				environment – being valuable for your company itself, the local 
				community and the society in general. Commitment from the 
				company to participate in sustainable development on this basis 
				is actually what defines corporate social responsibility,” says 
				Jan Jansen, CEO, Mawarid, the mining subsidiary of MB Holding.<br>
				<br>
				For Jansen, the term environment means people, planet and 
				profit. Explaining this, he says, On the one hand, you have the 
				economic realm and on the other hand you got the society within 
				which you have to operate. The economic and social realms are 
				bound by the environment. If you are looking at that framework, 
				then what do you want to achieve? You want to improve the 
				quality of life of your people and your community and at the 
				same time, you want to improve your business processes without 
				destroying the environment. “That is one of the cornerstones of 
				Mawarid’s philosophy. That forms one of the core values and 
				commitments of how we do our business – namely we shall look 
				after ourselves, the people around us and the world we live in,” 
				says Jansen.<br>
				<br>
				Mawarid has undertaken steps to minimise the damage to the 
				environment through its mining activities by building a fully 
				compliant modern tailings dam facility. Tailings dams are often 
				the most significant environmental liability for a mining 
				project and Mawarid has taken extreme care to prevent any 
				environmental damage. The water collected from the tailings dams 
				is being recycled and returned to the concentrator plant while 
				the plastic covered dam ensures that the ground water is not 
				polluted by any seepage from the tailings.<br>
				<br>
				<b><font size="2" color="#008000">Community influence</font></b><br>
				<br>
				Some companies are inspired by the community they live in, the 
				country they work in and the leader of the country when they 
				think about going green. “In this regard, His Majesty Sultan 
				Qaboos bin Said and the government are very clear in issuing 
				directives in order to look after the environment of this 
				beautiful country. We have talked to our teams to raise 
				awareness about the environment,” says Michael E Hansen, 
				managing director, Oman Marketing &amp; Services Co (OMASCO).<br>
				<br>
				“I am actually delighted to tell you that some of the European 
				companies based in Oman, like Carillion have actually bought 
				Hondas cars that have higher energy efficiency and less 
				emissions,” Hansen mentions. OMASCO has been mainly influenced 
				by its two key principals, namely Honda and Panasonic who are 
				saying consistently that going green means making products that 
				are recyclable and those that have less of an impact on the 
				planet and its resources. Both of Omasco’s principals are also 
				looking to improve energy efficiency in their products.<br>
				<br>
				Panasonic is quite advanced in testing products that have 
				photovoltaic cells to collect solar power for recharging dry 
				cell batteries. Such energy will power their plasma TV, the 
				Hi-fi system or any other Panasonic products. In some cases, 
				they are dreaming of producing enough electricity to sell to the 
				grid. Panasonic already has launched its Eco-navi 
				airconditioners that significantly reduce the consumption of 
				electricity. In conclusion, we can say that the green map of 
				Oman has been charted out and organisations have only to set out 
				on this journey to realise the socio-economic values of being in 
				harmony with their environment.<br>
