						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Meaning makes difference</strong></span><p class="style15">
				<strong><span class="style14">Dave Ulrich, internationally 
				famous management guru and Professor of Business at University 
				of Michigan, was recently in Muscat to attend a workshop 
				organised by Synerggie Eventz. In an interview with Muhammed 
				Nafie, he examines how organisations can build capabilities of 
				leadership through leveraging human resources<br>
&nbsp;</span></strong><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview1-april2011.jpg" style="border: 1px solid #808080; float: right;" class="style28" width="150" height="197"></font><b>You 
				advocate that leaders must pay as much attention to creating 
				meaning for employees as they do to making money. How do you 
				think organisations can implement your leadership model?<br>
				</b><br>
				When employees find meaning in their work, they will be 
				committed to the work; it will also prompt them to work harder 
				and become more productive. Obviously, this productivity will 
				lead to the customers being more connected to the company, 
				which, in turn, will add to the profitability. So there is a 
				value chain between meaning, productivity, customer satisfaction 
				and profitability. That value chain starts when the employees 
				find a sense of meaning or purpose in the work they are doing.<br>
				<br>
				<b>What are the challenges that a leader faces while trying to 
				offer deeper meaning to the employees?</b><br>
				<br>
				I think one of the biggest challenges is recognising that each 
				employee derives meaning from different things and different 
				sources. What provides meaning to you may be different from what 
				provides meaning to me. This is what we can call customisation 
				or personalisation. Another thing is to make sure that the 
				organisation is not just a social organisation where people find 
				meaning; but the meaning that people find has to contribute to 
				the success of the company. It means that meaning makes money. 
				There has to be that linkage.<br>
				<br>
				<b>Can you elaborate a bit on your concept of ‘abundant 
				organisation’?<br>
				</b><br>
				We want a word to capture what happens in a company when 
				employees find their sense of meaning and purpose. There are a 
				lot of other words we could have used such as purposeful 
				organisation or connected organisation. But I would like to use 
				the word ‘abundance’ because it means plenty and even more. 
				People will do more than what they could otherwise do and when 
				people find abundance they could fulfill what they strive to 
				accomplish. Hence I picked up the word abundance which captures 
				this message in a better way.<br>
				<br>
				There are seven principles that the leaders could do to become 
				meaning makers and create an abundant organisation. The first 
				one is to build an identity which helps people to build on their 
				strength and to use their strength to strengthen others. The 
				second thing is to create a clear sense of purpose. An identity 
				relates to who I am while the purpose is associated with where I 
				am headed to. The purpose makes me do what matters to me. Then 
				comes the relationship -- do we really work with people we enjoy 
				working with and is there a sense of relationship and 
				connection? The fourth principle is the work environment. The 
				fifth one is the work itself. It relates to whether the employee 
				is doing the work he wants to do; where and when he does it and 
				if the characteristics of the work are important to him. The 
				sixth one is to learn to grow; i.e. whether the employee is able 
				to change and constantly see and learn new things while doing 
				the work. The last, but not the least, is to simply having fun 
				in and enjoying the wok.<br>
				<br>
				<b>What is different about your employee value proposition?</b><br>
				<br>
				A lot of old value propositions are based on money. But money is 
				not linked to any of our seven principles. I know that money 
				matters but what is important is the base amount. Once the 
				employees are paid the base amount, then what differentiates is 
				the meaning they find in the work. Money is important but is not 
				the most important.<br>
				<br>
				<b>What, according to you, is the best way to assess whether an 
				organisation is creating meaning for its employees?</b><br>
				<br>
				The best way for a leader is to walk around and find out whether 
				his employees are feeling passionate, engaged and excited about 
				the work they are doing. When they are feeling those things, you 
				can probably sense the passion and energy that exist in the 
				organisation.<br>
				<br>
				<b>What are the most important challenges facing the leadership 
				in the 21st century?</b><br>
				<br>
				I have identified five major challenges. The first one is the 
				strategic challenge related to global markets, new customers, 
				emerging markets, new products and innovations. The second is 
				how to get there -- the challenge of execution. The third 
				challenge is getting the talented person, which also includes 
				the factors I have already mentioned. It is not only getting 
				people who have the right talent but also with a sense of 
				meaning, purpose and dedication. The fourth is to build the next 
				generation- to make sure that our employees have the skills for 
				the future. The fifth challenge is that of personal credibility 
				and having good grounding in values and ethics.<br>
				<br>
				<b>What is the most glaring shortcoming in the leader’s handling 
				of employee practices?</b><br>
				<br>
				Many leaders make a mistake when they look at the employees with 
				wrong expectations. For example, a leader cannot ask the 
				employees to enjoy whatever he likes for himself. A leader 
				should take into account the differences in sensitivities.<br>
				<br>
				<b>What are the competitive realties an organisation in this 
				region faces?</b><br>
				<br>
				The region is built mainly on oil. But I think several countries 
				are trying to diversify the economy away from oil resources 
				exploring new avenues of knowledge and service-based economy. It 
				is also important to make sure that the citizens have skills to 
				do jobs. The country has to increasingly educate and train its 
				human resources not just in general knowledge but in all 
				specific streams of knowledge such as science and maths to 
				enable them to take up the new challenges and help the country 
				move forward. Another important area is to make possible a 
				healthy interaction of various cultures. Singapore is good case 
				study on this.<br>
				<br>
				<b>What is your proposition for creating a more meaningful and 
				competitive work environment?</b><br>
				<br>
				Listen to your employees and find out from them what provides 
				meaning for them. Competiveness is made up of financial 
				discipline, great product strategy, great system and technology 
				and organisation and people. We cannot gloss over the fact that 
				an organisation is comprised of people, culture and leadership.
				<br>
