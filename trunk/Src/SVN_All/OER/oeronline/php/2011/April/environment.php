						<span class="style27"><strong>A green umbrella</strong></span><p class="style15">
				<strong><span class="style14">The second edition of Oman Green 
				Award, Oman’s first environmental award to honour outstanding 
				environmental vision, endeavours and achievements of corporates 
				and individuals, will be announced on June 5</span></strong><p align="justify" class="style13">
				Environmental issues and economic development increasingly 
				intersect and complement each other. Creating a green economy is 
				going to top the agenda of most corporates by the turn of this 
				decade as every company worth its salt strives to contribute its 
				mite to protect and restore what is left of the Earth and our 
				environment. As the world population increases, the demand for 
				food, energy, and luxuries has increased manifold. The first 
				casualty of catering to the burgeoning demands of the increasing 
				urban populace is the environment. Therefore, it is not only 
				corporate but also individuals and organisations who need to 
				contribute towards restoring the dwindling natural resource that 
				we call the Earth.<br>
				<br>
				Only an enhanced awareness campaign will stir interest and wake 
				up the people from general inaction and minimal activism to 
				positive environmental goals. The Oman Green Awards is a 
				significant step to create and increase environmental awareness, 
				as these will also lead towards greater action in the long term, 
				both at a community and decision making levels.<br>
				<br>
				<b>
				<img border="0" src="images/invir1-april2011.jpg" width="200" height="209" align="left" style="border: 1px solid #808080">For behavioural change<br>
				</b><br>
				As the country’s first Environmental Awards, Oman Green Awards 
				has been created to awaken the eco-consciousness in every 
				citizen and resident. It is aimed at honouring outstanding 
				environmental vision, endeavours and achievements of corporate 
				and individuals who matter most as they strive in this 
				environmental cause. The ultimate aim of the awards is to 
				motivate behavioural change and increase awareness in relation 
				to the protection and preservation of our environment. OGA 
				provides green groups a platform to create awareness, bring 
				about concerted action through exchange of ideas, and propel 
				others to come forth and participate in this social cause. The 
				second edition of OGA will be held on June 5, 2011.<br>
				<br>
				The Oman Green Awards is an ideal platform to honour, appreciate 
				and promote voluntary efforts and achievements. It is an 
				acknowledged fact that the local green activities and 
				earth-friendly efforts and achievements have not been 
				recognised. Globally and regionally, however, environmental 
				awards in governmental, corporate and other sectors have been 
				evolving for several years.<br>
				<br>
				Several organisations including corporate houses, institutions, 
				schools etc. are involved in relentless efforts to protect and 
				preserve the environment. But the lack of an umbrella 
				organisation that will bring these players together and 
				facilitate better coordination and exchange of ideas creates an 
				inherent lacuna in the system. The Oman Green Awards, as the 
				first of its kind in the Sultanate, quite naturally is expected 
				to facilitate steps to create a forum for coordinated 
				eco-efforts.<br>
