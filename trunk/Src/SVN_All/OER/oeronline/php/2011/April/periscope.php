						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Towards a bearish storm </font></b><p class="style15">
				<strong><span class="style14">It is not prudent to bottom fish 
				in the equities of the emerging markets such as China, India and 
				Brazil as they have been vulnerable to foreign selling, 
				political risk and oil shock</span></strong><p align="justify" class="style13">
				Emerging markets have underperformed Wall Street and Japan, as 
				fund managers, spooked by inflation and monetary tightening, 
				rotate into developed market equities. Valuation multiples have 
				contracted the most in countries where the central bank has been 
				perceived to be lagging the inflation curve, and local stock 
				market is vulnerable to foreign selling, political risk and the 
				oil shock. This was the perfect bearish storm for India’s Sensex, 
				which has lost more than 3,000 points since October as inflation 
				surged, the Reserve Bank of India (RBI) scrambled to tighten 
				policy, the ruling Congress coalition was hit by the 2G telecom 
				scandal and offshore funds slashed exposure to Dalal Street. 
				India’s valuation multiple contracted sharply from 21 to 17 
				times earnings, still a huge premium to the 12.5 multiple for 
				the Morgan Stanley emerging markets index.<br>
				<br>
				Political contagion in the Middle East and Brent crude at $120 
				will cause another round of FII and insurer selling in Indian 
				equities as well as exacerbate India’s balance of payment woes. 
				This means the Sensex can well fall to 16,000 in the next two 
				months. It is not prudent to bottom fish in Indian equities at 
				this time.<br>
				<br>
				<b>A bullish Russia</b><br>
				<br>
				Russia, of course, is the clear beneficiary of the surge in oil 
				prices and reduced EMEA allocations to the GCC, Egypt, Maghreb, 
				Levant and Turkish equities. Russia is also the cheapest major 
				emerging market, at 7.5 times current earnings for 30 per cent 
				EPS growth as the economy expands by 6 per cent and the black 
				gold windfall enables the Kremlin to engineer a Keynesian 
				pre-election government spending binge ahead of the Duma 
				elections. Russia also has negligible sovereign or mortgage debt 
				and is in the midst of an infrastructure bonanza as a prelude to 
				its WTO application. Strategic investors have been increasing 
				their exposure to Russia, as the recent Total stake in gas 
				exporter Novatek, the BP-Rosneft deal and acquisitions by Ford 
				Motor and Pepsi prove. Russia also boasts $500bn in hard 
				currency reserves and the rouble is the best performing 
				petrocurrency on the planet as oil prices surged by 25 per cent 
				with the onset of the Libyan uprising. Gazprom, owner of one 
				fifth of the world’s proven gas reserves and a major weight in 
				the Morgan Stanley Russia index, benefits hugely from the end of 
				the West European LNG glut and its own capex programme even 
				though it trades at below 6 times earnings. While risk aversion 
				due to Middle East geopolitics could trigger selloffs in Moscow 
				in the short term, the bullish case for Russian equities is 
				compelling. The Russian index fund (symbol RDX) in New York has 
				25-30 per cent upside as Moscow could well emerge as the best 
				performing emerging market of 2011, powered by a rouble surge 
				against the dollar, the Euro and the Japanese yen.<br>
				<br>
				<b>Bearish EWZ</b><br>
				<br>
				Brazil is Latin America’s economic colossus but its bellwether 
				Bovespa index is dominated by only three huge companies (state 
				oil firm Petrobras, miner Vale and bank Itau-Unibanco). While 
				the Bovespa has fallen from 71,632 to 66,000 now, it is still 
				premature to bottom fish in the Brazil stock index fund in New 
				York (symbol EWZ). After all, inflation is now 6 per cent, the 
				highest in six years. The central bank will be forced to raise 
				interest rates until inflation peaks, possibly sometime this 
				summer. A slowdown in Chinese GDP is negative for Brazil’s iron 
				ore exports to the Middle Kingdom. President Dilma Rousseff is 
				yet to have the international financial elite in thrall, unlike 
				her charismatic predecessor Lula da Silva. <br>
				EWZ will probably drift lower until inflation peaks, possibly as 
				low as 62-60. Yet Brazil’s valuation multiples are now entering 
				the value zone, at 11 times earnings, extremely cheap compared 
				to India’s 17X, Mexico’s 20X or even Chile’s 16X. Brazil’s macro 
				appeal will only be enhanced if finance minister Guido Montega 
				enacts a $30bn budget cut, though the firebrand union leaders of 
				the ruling Workers Party will pressure President Rousseff to 
				oppose it. This means earnings risk and a Bovespa that can 
				easily lose another 10 per cent. Stay short the Brazil index 
				fund (EWZ) until the summer, when it could well bottom somewhere 
				near 60.<br>
				<br>
				<b>China’s downside risk</b><br>
				<br>
				Inflation, the inevitable endgame of China’s post-Lehman 
				trillion dollar bank lending spree, the spike in diesel prices 
				and fruits/vegetables after the Schezwan floods, offshore hot 
				money lured by the undervalued and rising yuan and a property 
				bubble in Beijing and coastal provinces, is China’s 
				macroeconomic Count Dracula. The Chinese Politburo cannot forget 
				that hyperinflation enabled Chairman Mao’s peasant armies to 
				establish the People’s Republic in the first place and that high 
				inflation culminated in the riots that led to the Tiananmen 
				Square massacre in June 1989. So it is not surprising that the 
				PBOC, China’s central bank, has raised lending rates and hiked 
				bank reserve ratios as high as 19 per cent in a bid to control 
				parabolic money supply and credit growth. In addition, the 
				Chinese government has launched a crackdown against property 
				speculators, hoarding and price controls.<br>
				<br>
				Naturally, the Shanghai/Shenzehn stock market experienced 
				collateral damage in China’s battle against inflation. Yet CPI 
				has still not peaked and the deposit rate is still negative by 
				almost 200 basis points. However, China’s economic growth is now 
				slowing after the cumulative impact of six successive PBOC rate 
				hikes, with manufacturing growth at its lowest level in six 
				months. This means that the China/Xinhua index fund (symbol FXI) 
				still has downside risk as stratospheric crude oil prices are a 
				catastrophe for China’s exporters, whose margins and EPS growth 
				will be hit.<br>
