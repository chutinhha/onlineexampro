						Championing cause of change</font></b><p align="justify" class="style3">
				<strong><span class="style5">Dave Ulrich’s Human Resource 
				Champion suggests an innovative change of role for human 
				resource professionals in modern organisations, capable of 
				reshaping the entire field for years to come. A review by <i>
				Muhammed Nafie</i><br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/browsingcornenr1-april2011.jpg" width="200" height="281" style="border-style:solid; border-color:#808080; float: right" class="style4"></font>The 
				new competitive horizon dominated by globalisation has ushered 
				in new markets, new products, new mindsets, new competencies and 
				new ways of thinking about business. It has also brought forth 
				the necessity of a paradigm shift in the role of HR in creating 
				new models and processes capable of attaining global agility, 
				effectiveness and competiveness. With workforce competence and 
				organisational capabilities assuming centre stage in a rapidly 
				developing world, HR has emerged a highly purposeful and 
				innovative area with regards to understanding, leveraging and 
				crafting capabilities and competencies. But, cast in the same 
				old mould that has been in vogue for the last 40 years, most of 
				the discussions and debates on this revolved around what HR 
				people can do instead of deliberating a novel approach patterned 
				on what they can deliver. <br>
				Dave Urich’s Human Resource Champion challenges traditional 
				beliefs and assumption about HR professionals, HR practices and 
				HR departments. Persuading a shift of focus from doables to 
				deliverables, as Ulrich himself claims in the preface, the book 
				envisions an innovative, if not radical, change of role for 
				human resource professionals in modern organisations, capable of 
				reshaping the entire field for years to come. It focuses mainly 
				on the outcomes, guarantees and results of HR work and examines 
				how and what HR professionals can deliver.<br>
				<br>
				What is the value an HR professional is expected to create for 
				customers, employees and investors? The book identifies four 
				generic outcomes that can bring about a qualitative transition 
				in HR practices: strategy execution, administrative efficiency, 
				employee contribution and capacity for change. Ulrich provides a 
				vast array of useful tools that show HR professionals how they 
				can operate in all four areas simultaneously. With multiple 
				implication, these examples help HR professionals to leverage 
				their organisations’ human resource efforts for competitive 
				advantage. This information also provides HR professionals and 
				line managers a diagnostic with which to evaluate and improve 
				both HR practices and HR departments.<br>
				<br>
				The author maintains that the real human resources champions of 
				our time are those who can better understand and increase their 
				firm’s human and intellectual capital. To become a change agent 
				in a world where the pace of change is both dizzying and 
				dazzling, the HR professionals have to develop skills to respond 
				to the pace of change. Instead of being baffled by the 
				unanticipated change they confront, they have to exhibit the 
				ability to adapt, learn and respond.<br>
				<br>
				The shift of focus in HR practices to global management, 
				organisational capabilities, cultural change and intellectual 
				capital necessitates new initiatives, programmes and agendas. 
				Otherwise HR should be bounded by its past rather than judged by 
				its future.<br>
				<br>
				In short, as an analytical and research-oriented unde-rstanding 
				of HR, the book reiterates the importance of having a point of 
				view and a clearly defined and articulated set of beliefs, <br>
				of HR.<br>
