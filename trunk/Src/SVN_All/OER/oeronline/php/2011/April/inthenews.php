						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">A crippling burden</span></strong><p class="style15">
				<span class="style14"><strong>The recent unrest witnessed in the 
				Sultanate and the measures being taken to address protestor 
				demands have placed an enormous financial burden on both the 
				public and private sectors. <i>Mayank Singh reports</i></strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/inthnews1-april2011.jpg" width="400" height="231" style="border: 1px solid #808080"></font><p align="justify" class="style13">
				Protests and strikes have been the order of the day in Oman from 
				February 26 onwards. What started off as a one off demonstration 
				in Sohar soon caught onto other areas and organisations. The 
				government reacted swiftly by carrying out a number of reforms 
				(see Box 1: Oman Economic Reforms). While the heat and dust on 
				the first wave was settling down, Oman’s corporate sector was 
				hit by a series of lock-outs and roadblocks. Says Nabeel Sultan, 
				director, Jawad Sultan Group of Companies, “It is a classic case 
				of ‘stimulus and response’, something was demanded and it was 
				rewarded. So what started on a scale of one escalated to level 
				four or five and it has now come down to fulfilling everyone’s 
				dreams and wishes.”<br>
				Speaking to OER’s sister publication Alam al-Iktisaad Wal A’mal 
				(AIWA), HE Khalil Al Khonji, chairman, Oman Chamber of Commerce 
				and Industry says, “A number of demands of employees are 
				genuine. In various companies there was no mechanism for the 
				demands of the lower rung employees to be conveyed to the higher 
				management and that needs to be done.”<br>
				<br>
				As the government and the private sector get down to addressing 
				various issues, things may settle down soon, but the financial 
				costs of these measures will reverberate for a long time to 
				come. While exact numbers are difficult to ascertain, we try and 
				look at the economic implications of the demonstrations and the 
				reforms that were carried out in its wake. <br>
				<br>
				<b>PUBLIC SECTOR</b><br>
				<b>Employment measures<br>
				</b><br>
				On February 27, His Majesty Sultan Qaboos bin Said issued orders 
				to create jobs for 50,000 nationals in 2011. A back of the 
				envelope shows that if 35,000 jobs were created in the public 
				sector at an average rate of RO300, then it would cost the 
				exchequer RO126mn in a year. The government has also announced a 
				grant of RO150 per month to each job seeker registered with the 
				Ministry of Manpower until he or she finds a job. According to 
				the CIA World Factbook, unemployment rate in Oman as of December 
				20, 2010 stood at 15 per cent. This pegs the number of 
				unemployed Omanis at 120,000. If each one of them are paid RO150 
				from March 2011 onwards, it would entail an additional budgetary 
				expenditure of RO180mn this year.<br>
				<br>
				<b>Social security measures</b><br>
				<br>
				The monthly pension of Omani government employees who are 
				covered by the post service pensions and remunerations law was 
				increased by 50 per cent for those in the lowest salary category 
				of RO135-200; the pension pay for those belonging to the 
				RO200-400 category has been increased by 40 per cent; the 
				RO400-600 category gets a 30 per cent hike; the RO600-1,000 
				recipient will get a 15 per cent raise and those who receive 
				over RO1,000 get a five per cent increase. The increases work 
				out at RO75-150 range. Overall, 35,729 pensioners are expected 
				to benefit from the measure. On a conservative note, if all of 
				them get RO75 more per month it would cost an additional RO32mn 
				per annum.<br>
				<br>
				A Royal Decree also increased the social security pension in the 
				Sultanate. Approximately 51,442 people are expected to gain from 
				the scheme. If each of these pensioners drew RO75 more per 
				month, it works out at RO46mn per year. <br>
				<br>
				<b>Students welfare</b><br>
				<br>
				The government has increased the monthly financial allocation 
				for students in public colleges, institutes and centres 
				affiliated to the Ministry of Higher Education and the Ministry 
				of Manpower. This may lead to an expenditure of around RO1mn per 
				annum. <br>
				<br>
				If we put together all these figures it works out at RO385mn. 
				This enhanced and unexpected burden translates into a 45.29 per 
				cent increase in the projected budget deficit of RO850mn for 
				2011. The deficit is based on a projected oil price of $58 per 
				barrel in 2011, but with crude prices hovering at the $100 per 
				barrel mark, Oman should comfortably end up with a surplus 
				budget despite the additional expenditure. Oman and Bahrain also 
				have a choice to borrow $1bn each from a GCC fund every year for 
				the next ten years to tide over the crisis. The Sultanate, 
				though, has not indicated whether it would borrow from the fund 
				as of now.<br>
				<br>
				<b>PRIVATE SECTOR</b><p align="center" class="style13">
				<img border="0" src="images/inthnews2-april2011.jpg" width="496" height="339" style="border: 1px solid #808080"><p align="justify" class="style13">
				<br>
				According to a report in a leading newspaper, international 
				economic reports peg the total material losses as a result of 
				the demonstrations, disruption of work and stoppage of 
				production at $1bn (RO387.59mn). And there may be more bad news 
				for the private sector as it gets down to estimating the total 
				dent on its topline and bottomline. <br>
				<br>
				<b>Staff costs</b><br>
				<br>
				The ministerial decision No 77/2011 has fixed the minimum wage 
				for Omanis in the private sector at RO200 per month from March 
				1, 2011. The minimum wage till now was RO120. According to the 
				Public Authority for Social Insurance (PASI) January 2011 
				figures, close to 113,793 Omanis who were drawing RO180 or less 
				as salaries are expected to benefit from the decision. The 
				private sector will have to take on an additional burden of 
				RO78.89mn annually as a result. A CEO of an export oriented 
				manufacturing unit located at Rusayl Industrial Estate (RIE) 
				says, “Our profits for 2011 will drop by almost 50 per cent.” He 
				runs us through some other numbers that give a bird’s eye view 
				of what companies in Oman may be in for. Factories in RIA have 
				been asked to move from six to a five-day working week, which 
				leads to a 16 per cent loss of production in manufacturing 
				plants. As the Omani Labour Law fixes the maximum number of 
				working hours a week at 48, companies would be forced to hire 
				more employees to keep their factories running at the same 
				production levels.<br>
				<br>
				In addition, companies have been asked to give RO50 as a cost of 
				living allowance for employees getting a salary of less than 
				RO500 and RO30 for people drawing more than RO500. <br>
				<br>
				A number of companies outsource safety services to Royal Oman 
				Police (ROP) and with ROP increasing staff salaries, such 
				outsourced services would cost more. The same applies to freight 
				and forwarding, packaging and all other intermediate services. A 
				number of export-oriented units have seen a major cancellation 
				of orders from European companies. “Our profits and dividends 
				will drop drastically and smaller units may actually close 
				down,” says the CEO from RIE. <br>
				<br>
				Companies operating in the oil and gas space are facing a 
				similar predicament. For example, Petroleum Development Oman (PDO) 
				has directed its contractors to start paying its employees a 
				minimum wage of RO270. In addition, the contractor needs to pay 
				a cost of living allowance; everything put together raises the 
				minimum wage at the entry level from RO195 to RO395. The oil 
				major has also directed contractors to move from the prevailing 
				20 days work, 10 days leave cycle to a 14- day work and 14-day 
				leave cycle. This will impose an additional financial burden on 
				contractors as they will be forced to recruit more staff. On the 
				positive side, PDO has set up a claims committee wherein 
				contractors can share their concerns and ask for better terms on 
				existing contracts. In future contracts the higher costs are 
				expected to be in-built during the tendering process itself. <br>
				<br>
				The senior vice president of a brokerage firm adds, “The impact 
				of the crisis will vary from sector to sector but on an average 
				there will be a 20-25 per cent impact on the bottomline. 
				Production costs would go up and most companies would try to 
				pass on the extra spends leading to an increase in service and 
				product costs.” Beleaguered companies are now asking for a 
				number of concessions like tax breaks, reduction in levies, 
				duties, rentals and electricity and water rates from the 
				government. The government is expected to agree to some of these 
				demands. <br>
				<br>
				<b>Foreign Direct Investment </b><br>
				<br>
				The unrest in the region has led to stability concerns amongst 
				foreign investors. In the last three months Foreign 
				Institutional Investors (FII’s) have pulled out RO40mn from 
				Oman. <br>
				<br>
				Standard &amp; Poor’s Ratings Services (S&amp;P) put Oman on watch for 
				downgrade on March 7, on the back of the investment community’s 
				concern over political unrest in the Middle East. S&amp;P warns in a 
				statement, “Current social unrest in the country and the 
				possible repercussions of regional conflicts could increase 
				political risks in Oman, which might eventually also negatively 
				affect economic growth and public finances.” The rating agency 
				has Oman at A, midway between junk territory and its coveted AAA 
				rating. A downgrade may increase the cost of dollar borrowing 
				for local banks in the international market. It can also 
				adversely impact the flow of FDI into the country in the short 
				to medium term. <br>
				<br>
				<b>Tourism takes a hit</b><br>
				<br>
				“Travel and tourism is a discretionary spending. The moment the 
				protests started in February we started receiving cancellations 
				from all over Europe. For an international traveller, Algeria, 
				Egypt, Tunisia and Oman are one big whole. We have seen 
				three-fourths of our business being cancelled and this amounts 
				to $75,000O (RO28,839),” says the CEO of one of the largest 
				travel agencies in the Sultanate. Countries like Australia, USA 
				and UK issued travel advisories asking their citizens to 
				exercise caution while visiting Oman, further scaring tourists.
				<br>
				<br>
				A number of cruise liners like Aster were asked to go away from 
				Oman due to unrest and protest at ports. The fact that this has 
				come during the peak season running from September to April has 
				created a major problem for the hospitality sector. Hotel 
				occupancies which would normally hover around 75 per cent during 
				these months have come down to 35 per cent. The hospitality 
				sector is expected to take a RO5mn hit. The Ministry of Tourism 
				has set up a committee to look into the matter and some of the 
				measures being contemplated include bringing a group of 
				international journalists to Oman to report on the on-ground 
				situation. A reputed international PR agency has been appointed 
				to project positive state of affairs in Oman.<br>
				<br>
				<b>The silver lining</b><br>
				<br>
				While the short term burden may be daunting for companies, there 
				is general agreement that a number of these measures will have a 
				positive fallout on the economy. Says Ahmed Qadir al Balushi, 
				general manager, Habib Bank, “The new measures will create a 
				RO1.5bn opportunity over the next five years.”<br>
				<br>
				The credit book of banks and leasing companies are expected to 
				grow as new jobs and increased salaries generate demand for new 
				loans. The medium term (next 2 years) will see strong demand for 
				cars and consumer durables, while in the longer term (3-5 years) 
				there will be a demand for housing loans. Says Nabeel, “Reduced 
				margins should hopefully be compensated by higher demand.” More 
				money in the system will lead to inflation, but that is an issue 
				for the future. The government on its part is already working on 
				measures to regulate the prices of essential commodities. “The 
				country has seen more change in the last one month than in the 
				last 20 years, but there are other issues that cannot be 
				addressed overnight,” says an industry leader. The need of the 
				hour is patience and a belief in the government. The question is 
				whether people are amenable to reason anymore?<br>
				<br>
				<i>With inputs from Visvas Paul D Karra</i><br>
