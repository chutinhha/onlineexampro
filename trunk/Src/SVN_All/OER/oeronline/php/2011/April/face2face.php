						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Shifting the balance</span></strong><p class="style15">
				<span class="style14"><strong>British Minister of Trade and 
				Investment, <i><u>Lord Steven Green</u></i>, who was on a recent visit to 
				Oman, speaks about Oman-UK ties as well as how the world 
				economies should rethink their strategies. Excerpts of an 
				exclusive interview with<i> Visvas Paul D Karra</i></strong></span><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face1-april2011.jpg" width="150" height="218" style="border: 1px solid #808080; " align="left"></font><p class="style15">
				The future economic strategy for countries around the world 
				should be to find a balanced growth path between consumption, 
				borrowing and trade in order to avoid the pitfalls of the global 
				financial crisis, says Lord Steven Green, British Minister of 
				Trade and Investment, who was in Oman recently.<br>
				<br>
				One particular lesson learnt from the financial and economic 
				crisis is that financial imbalances make way for instability in 
				the global financial system. For example, a number of big 
				countries had become too dependent upon consumption, borrowing 
				and imports while other big countries had become too dependent 
				on exports and investments with not enough consumption.<br>
				<br>
				This created financial imbalances and contributed to the global 
				crisis. At a global macro-economic level, finding a balanced 
				growth path going forward is important but this is a task, which 
				cannot be achieved in one year, Lord Green adds. It is also 
				important for the world to take a growth path, which is less 
				carbon intensive and more greener, he added.<br>
				<br>
				Discussing financial fundamentals is not new to Lord Green, as 
				he was the chairman of HSBC Bank before retiring and going on to 
				become the British Minister of Trade and Investment. HSBC was 
				one of the few international banks, which stood tall while its 
				counterparts fell like nine pins in the aftermath of the global 
				financial crisis sparked by the US sub-prime crisis, whose root 
				cause lay in financial imbalances.<br>
				<br>
				“It is good to be back in Oman with a different hat on after 
				coming here in March 2010 as Chairman of HSBC,” says Lord Green 
				who has been a regular visitor over the years to Oman. This time 
				around though, he was part of the trade and business delegation 
				that accompanied British Prime Minister James Cameron on his 
				visit to Oman in February.<br>
				<br>
				CEOs and top executives representing more than 20 UK companies 
				spanning, among others, the educational, finance, oil and gas, 
				and professional services sectors, were represented on the 
				delegation.<br>
				<br>
				<b>Investment agenda</b><br>
				<br>
				“The bilateral trade between Oman and Britain had doubled in the 
				last five years with British exports to the Sultanate of Oman 
				increasing 20 per cent to £415mn in 2010 as compared to the 
				previous year. Also, trade between the two countries was worth 
				nearly £550mn in 2010, an increase of over 18 per cent in 2009,” 
				Lord Green told reporters after addressing the UK-Oman business 
				forum.<br>
				<br>
				Lord Green admitted the UK private sector did not focus much on 
				Oman but it is time to pay more attention to power, real estate 
				and infrastructure projects in Oman. Incidentally, the UK also 
				needs investments in similar sectors, disclosed Lord Green.<br>
				<br>
				Britain is the biggest foreign investor by far in Oman’s 
				economy, with investments concentrated mainly in the 
				hydrocarbons and financial services. On the other hand, 
				state-owned investment vehicles, such as the State General 
				Reserve Fund (SGRF), Oman Investment Fund (OIF) and Oman Oil 
				Company (OOC), have ventured into the UK. SGRF, in particular, 
				already has investments in UK real estate and pharmaceuticals.<br>
				<br>
				Oman has increased its spending to about $21bn in 2011, up from 
				about $18.5bn in 2009 on development projects. Lord Green’s 
				desire is that Britain should be a part of Oman’s five-year 
				development plan and the country’s development, helping it with 
				its infrastructure.<br>
				<br>
				“When I hear of Omani ministers talk of investments in ports, 
				power, and infrastructure, I feel that we have similar 
				investments agenda because UK is also looking for such 
				investments. We have a clear investment map which totals to 
				around £400bn over a ten year period. That includes transport, 
				energy, grid distribution, water, broadband and digital 
				communications,” Lord Green says.<br>
				<br>
				<b>Special ties<br>
				</b><br>
				Talking about the special relationship being enjoyed by Oman and 
				the UK, Lord Green said the British trade delegation was here to 
				celebrate the long standing relationship being enjoyed by the 
				two countries and this was evident in the stature of members 
				making up the first British trade mission to visit the Sultanate 
				after a gap of 16 years. Despite such strong ties, it is an 
				embarrassment to know of this gap, Lord Green admitted, while 
				saying that British companies need to ask themselves whether 
				they have paid enough attention to Oman considering that the 
				Sultanate has a very stable environment in terms of ease of 
				doing business.<br>
				<br>
				Citing an example of the attractiveness of Oman as a business 
				destination, Lord Green spoke about a point made by Mothercare, 
				the UK retailer which has developed a substantial business with 
				their Kuwaiti partner in the region, and has made Oman a 
				transhipment base. As Oman becomes more of a hub for 
				transhipment because it faces both eastwards and westwards, 
				there are opportunities through that role for Oman to play a 
				bigger role in their supply chain. If Mothercare is having 
				success in Oman through their retail chain, then other British 
				companies can think along similar lines, Lord Green says.<br>
				<br>
				Lord Green also opined that London’s status as the financial 
				capital of world has not changed and it will remain as such 
				despite the economic meltdown because the city has basic 
				competitive advantages like the time zone, English language etc.
				<br>
				<br>
				<b>Long career<br>
				</b><br>
				Lord Green’s career began in the 70s with the British 
				Government’s Ministry of Overseas Development and took on 
				various roles as consultant and banker.<br>
				<br>
				He joined HSBC in 1982 with responsibility for corporate 
				planning activities. In 1992, he became group treasurer of HSBC 
				Holdings plc, with responsibility for the HSBC Group’s treasury 
				and capital markets businesses globally. He became group chief 
				executive on June 1, 2003 and group chairman on May 26, 2006.
				<br>
				<br>
				Lord Green became chairman of the British Bankers’ Association 
				in November 2006. In May 2010, he was elected deputy president 
				of the Confederation of British Industry and in July was elected 
				vice-chairman of the International Chamber of Commerce.<br>
