						<span class="style27"><strong>A carnival of bibliophiles</strong></span><p class="style15">
				<strong><span class="style14">The 16th edition of Muscat 
				International Book Fair has demonstrated the dynamism and 
				diversity of the Arab publishing industry which produces 
				approximately 60,000 new titles each year, reports Muhammed 
				Nafie </span></strong><p align="justify" class="style13">
				<img border="0" src="images/event1-april2011.jpg" width="200" height="170" align="left" style="border: 1px solid #808080"><p align="justify" class="style13">
				Egyptians write, Lebanese publish, Iraqis read”, famously said 
				Taha Hussein, an illustrious Arab modernist writer and 
				intellectual of the 20th century. But you will strongly disagree 
				with this celebrated maxim about Arab publishing industry while 
				standing inside Al Farahdi hall, named after the author of the 
				first Arabic dictionary, at Muscat International Book Fair, 
				where you would be greeted by around 400 publishers from across 
				the length and breadth of the Arab world.<br>
				<br>
				Spread over 677 stalls, a thousand gazillions of books ranging 
				from the paperback translations of the latest English 
				blockbusters to voluminous reference materials, encyclopaedic 
				research works and philosophical books, demonstrate the richness 
				and diversity of the Arab publishing industry which produces 
				approximately 60,000 new titles each year. Though Lebanon and 
				Egypt still far outnumber other countries, as usual in any Arab 
				book fair, Arab publishers from over 30 countries, which include 
				a few western countries, display the cultural and literary 
				flavours of their countries in the 16th edition of the fair.<br>
				Though the publishing industry in the Arab world, as elsewhere, 
				was hit by the internet and visual media, some leading 
				publishers felt that the internet had helped boost sales through 
				reviews and articles. The book exhibitions are gaining 
				popularity all over the Arab world with a steady rise in the 
				number of exhibitors and visitors in every year, says Ziyad from 
				Dar Usama publishing house, Lebanon. “Though we could not sell 
				more titles during these ten days -- may be we have sold less 
				number of books compared to the previous year, book fairs help 
				us to reach out to more people and expand our influence among 
				the prospective readers,” he adds.<br>
				<br>
				<b>Mosaic of cultures</b><br>
				<br>
				The titles displayed exemplify not just the dynamism of the Arab 
				publishing industry but also the diversity and adaptability of 
				the Arabic as a cosmopolitan language. A relaxed stroll across 
				the stalls would give you a glimpse of how Arabic has evolved as 
				a multi-cultural and multifaceted language over a period. For 
				instance, you could see the anthologies of Ahmed Shawqi and 
				Mahmoud Darwish lying in close proximity to the translated works 
				of Milan Kundera and Pablo Neruda; and classical Islamic 
				jurisprudential works, published by Lebanon’s Dar al Fikr, 
				interspersed with the translated works of western philosophers 
				like Foucault and Frantz Fanon and neocon political theorist 
				William Kristol.<br>
				<br>
				According to Yousuf Bin Ibrahim Al Balushi, director of the 
				fair, not only the number of foreign publishers but also that of 
				visitors has increased substantially this year. Testifying to 
				this statement was a cross section of readers and booklovers one 
				met across the stalls – whether it be an Omani student of 
				comparative literature in a foreign university buying an Arabic 
				translation of Indian novelist Arundhati Roy’s Booker 
				prize-winning God of Small things, an elderly bibliophile 
				picking up translation of Russian classics and Omani girls 
				making beelines to buy translation of latest management books.<br>
				<br>
				Arab publishers from non-Arab countries such as Germany, UK, US, 
				Sweden, Turkey and Iran also attracted a lot of book lovers. 
				“Iranian stalls at the book fair provide us with a rare 
				opportunity to collect new titles in Shi’ite literature,” says 
				Hasan Al Lawati, a visitor.<br>
				<br>
				However, a number of publishers had a different opinion. “The 
				on-going protests in various parts of the country have adversely 
				affected the sale. We had a poor sale this year, compared to the 
				last year. Even during the weekend, the turnout was very law,” 
				says Noufal Saqr from Dar Al Sharooq publishing house, Egypt. 
				Bin Duried hall dedicated for English publishers and Al-Outabi 
				hall for various government institutions from inside and outside 
				the Sultanate, also wore a disappointing look. “Since a lot of 
				people here prefer Arabic translations to their English 
				originals, we cannot expect them to visit our stalls”, quips a 
				bookseller from Al Mutanabbi Bookshop.<br>
