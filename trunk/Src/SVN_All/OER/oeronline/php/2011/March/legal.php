                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					<br>
					Omani Arbitration Law<br>
					</font><font face="Verdana" size="2"><br>
					Omani court case may go through three tiers of justice 
					whereas an arbitral award is final, with no right of appeal<br>
					<br>
					</font></b><font face="Verdana" size="1">There is often a 
					lack of understanding about how Oman’s Arbitration Law 
					(Royal Decree 47/97 as amended) works. The truth is that 
					this law provides a viable alternative to having a dispute 
					heard by the Omani courts.<br>
					<br>
					The fundamental difference between the Omani court system as 
					opposed to the Omani arbitration system is that an Omani 
					court case may go through three tiers of justice (primary, 
					appeal and supreme), whereas an arbitral award is final, 
					with no right of appeal, and it is extremely unlikely that 
					the Omani courts would nullify an arbitral award. This means 
					that almost certainly an arbitral award will lead to the end 
					of the dispute, without any ability to appeal or overturn 
					the arbitral award.<br>
					<br>
					The best time to agree on arbitration is in the contract at 
					the start of the relationship with your counter-party. For 
					instance, if party A desired to have any dispute heard by an 
					arbitral panel, using Omani Arbitration Law as the procedure 
					for such arbitration, it is best for the contract to state: 
					“Any dispute will be finally settled via arbitration in 
					accordance with Omani Arbitration Law (Royal Decree 47/97
					<br>
					as amended).”<br>
					<br>
					However, the above clause can be further fine-tuned to state 
					how many arbitrators will be on the arbitral panel. Article 
					15 of Oman’s Arbitration Law states that if the parties have 
					not agreed on the number of arbitrators, the number of them 
					will be three. Accordingly, if the parties wanted a sole 
					arbitrator to hear any dispute, they should say so in the 
					contract. It is worth bearing in mind that with a panel of 
					three arbitrators, a majority 2-1 decision is sufficient, 
					unless the parties had agreed otherwise in writing before 
					the commencement of the arbitration.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Timely resolution</b></font><font face="Verdana" size="1"><br>
					<br>
					Article 17 states that the court would decide the identity 
					of the sole arbitrator, if the parties cannot agree on whom 
					it should be. If the panel is to consist of three 
					arbitrators, one party states his nominee as arbitrator, and 
					the other party must then state his nominated arbitrator 
					within the following 30 days. The two arbitrators thus 
					chosen should select a third arbitrator within 30 days after 
					the nomination of the second arbitrator. However, either 
					party can apply to court if these 30 day deadlines are 
					breached.<br>
					<br>
					An important part of the Omani Arbitration Law is what it 
					states about the time procedural framework for an 
					arbitration case. It is often forgotten that Article 27 says 
					that – unless the parties have agreed otherwise - the 
					arbitration clock starts ticking on the day the defendant 
					receives a letter from the claimant stating that a dispute 
					exists and that, as per the contract, it must be resolved 
					via the agreed arbitration procedures. Article 45 requires 
					that the arbitral award must be forthcoming within 18 months 
					after the clock started to tick, unless the arbitral panel 
					and the parties agree otherwise.<br>
					<br>
					Another salient aspect of Omani arbitration is that the 
					arbitral panel can appoint one or more experts to present a 
					report. The Law states that the parties will be given the 
					opportunity to present their views on the expert reports. In 
					addition, each party has the right to examine the report of 
					the expert. The parties are also granted the right to bring 
					their own experts to an arbitral hearing, to present their 
					views on the contents of the reports prepared by the expert 
					appointed by the arbitral panel.<br>
