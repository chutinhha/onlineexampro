					<br>
					</font></b><font face="Verdana" size="1">With real Estate 
					prices in Oman correcting from the astronomical levels that 
					they had reached in mid 2008, It will be educative to look 
					at the factors that took the prices to historical highs and 
					examine the reasons. Although they have corrected, they have 
					now settled at much higher levels than they were seven to 
					eight years ago. There are a few interesting questions that 
					may shed more light on the lessons that the real estate 
					industry needs to learn at this juncture: Was it speculation 
					driven by liquidity, was their increased demand affecting 
					fundamentals, was there a supply constraint and how much of 
					a factor was inflation? It is important to learn in order to 
					avoid the severely damaging real estate bubbles that have 
					crippled economies around the world. Oman is a natural 
					resource-based economy and economic growth and real estate 
					prices move in tandem with oil prices. The chart given below 
					shows how the price of a typical commercial/residential plot 
					in the prime area of Al Khuwair has moved with oil prices 
					over the years. <br>
					<br>
					There is a very close correlation between oil prices and 
					real estate prices. This is because high oil prices allow 
					higher government outlays which, in turn, drives economic 
					growth and increases the demand. High oil prices will make 
					market participants optimistic about the future prompting 
					them to bid up prices. One of the prime factors behind real 
					estate price increase is economic growth. To a certain 
					extent economic growth depends on oil prices. The nominal 
					per capita income has doubled from RO3,000 in 2000 to about 
					RO6,000 in 2010. There is about 35 per cent inflation built 
					in to the nominal per capita incomes. Higher incomes lead to 
					greater demand which pushes up the prices. <br>
					<br>
					The relative share of building and construction activity to 
					the GDP has gone up from about 2 per cent in 2000 to about 6 
					per cent in 2009. The country spends more percentage of its 
					GDP on construction than it uses to pushing up demand. Thus 
					real estate prices have gone up faster than the growth in 
					nominal GDP. The more jobs the economy creates the more will 
					be the real estate demand. The number of employees in the 
					private sector has gone up from about 500,000 in 2005 to 
					about 1,100,000 today thus creating demand for more 
					properties.<br>
					<br>
					So we cannot gloss over these fundamental factors that have 
					driven up real estate prices and rents. However, there was 
					an element of speculation in mid-2008 when irrational 
					exuberance took over the markets. In that short period, 
					prices were divorced from fundamentals. Central banks try to 
					prevent asset bubbles; but that becomes very difficult to do 
					when euphoria takes over market participants.<br>
