	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; ">
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/obituary-1.jpg" hspace="8" vspace="5" style="border: 2px solid #C0C0C0; " align="right" width="150" height="157"></font></b><p class="MsoNormal">
				<b><font face="Verdana" size="2">Here we produce a first person 
				account on the life of the late HE Dr </b><i>Rajiha bint Abdulamir bin 
				Ali</i><b>, Minister of Tourism, as told by her husband 
				</b>
				<i>Murtadha Hassan Ali</i><b>, Chairman, Genetco and 
				</b>
				<i>Faisal Murtadha Al Lawati</i><b>, executive 
				director, Genetco, her son.<br>
				<br>
				</b>
				</font><font face="Verdana" size="1">They say that those 
				whom the gods love die young. In the case of HE Dr<i> Rajiha 
				bint Abdulamir bin Ali,</i> Minister of Tourism, she was beloved 
				to the people of Oman as well. Dr Rajiha, who took the helm of 
				the nascent ministry of tourism in 2004, was fully aware that 
				her portfolio was very critical for the development of the 
				national economy. The road was tough and the journey long, but 
				Dr Rajiha had boundless energy to steer the ministry with the 
				Omani mast flying high. The results of Dr Rajiha’s tireless 
				efforts are there for everyone to see. Oman’s name is now firmly 
				stamped on the world tourism map and the contribution of the 
				tourism sector to the GDP of Oman has exceeded the set targets. 
				Dr Rajiha’s death is an irreplaceable and profound loss to the 
				nation. Her dedication and commitment to her work was so 
				remarkable that she continued to discharge her ministerial 
				duties even from her hospital room till she breathed her last, 
				says her son Faisal.<br>
				<br>
				<b>Murtadha Hassan Ali:</b><br>
				<br>
				After her graduation from Baghdad, Rajiha came back to Oman in 
				1972 and started her career as an inspector at the Ministry of 
				Education. At that time, she was the youngest inspector at the 
				ministry. Most of her colleagues found it very odd to work with 
				some one who was at least twenty years younger than them. This 
				forced HE Sheikh Walid Bin Zaher Al Hinai, the then Minister of 
				Education, to change her position from the post of inspector to 
				a mathematics teacher. But still she had to teach students who 
				were older than her, which she found difficult. At that time, a 
				United Nations delegation visited the Sultanate and decided to 
				set up a statistics centre in the country. Of around 50 people 
				who were considered for the post of the director of the centre, 
				she was selected. <br>
				<br>
				After a short while, Rajiha was chosen to represent the 
				Sultanate at an important international conference. She was so 
				confused about the trip that she sought my advice, though we 
				were not married at that time. I asked her to go as it would 
				open up new vistas in her professional life. During the 
				conference she met a number of prominent personalities who later 
				became ministers and senior officials in their respective 
				countries. But she was the only Arab woman representative in 
				that conference which was held in 1973, a fact which testifies 
				to the importance the Sultanate accorded to women empowerment. 
				This conference was a milestone in her life because she came 
				back a different person with great confidence and enthusiasm.
				<br>
				It was during this time that the Development Council was 
				established in Muscat which was later replaced with the Ministry 
				of Commerce and Industry and the Ministry of Agriculture, Oil 
				and Minerals. Rajiha was appointed as the director in charge of 
				statistics at the Ministry of Commerce. Later, when the 
				Development Council was reconstituted she was a part of it. It 
				was in that period that we got married. </font></p>
				<p class="MsoNormal" align="center">
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/obituary-2.jpg" hspace="8" vspace="5" style="border: 2px solid #C0C0C0; " width="400" height="170"></font></b><font face="Verdana" size="1"><br>
				&nbsp;</font></p>
				<p class="MsoNormal" align="justify">
				<font face="Verdana" size="1">
				<br>
				Then she visited the US and came back to become the director 
				general of statistics at the ministry. <br>
				<br>
				In 1987, she became the senior undersecretary for planning 
				affairs at the Ministry of Commerce. But before she was 
				appointed as the undersecretary, she had been offered the post 
				of Executive Vice President of the Central Bank of Oman (CBO) 
				with an attractive salary. But she categorically declined that 
				offer saying that it was not her cup of tea. After that she got 
				another attractive offer. She was invited to head the 
				Organisation for Economic Cooperation and Development which was 
				looking for a woman from a GCC country for this post. But Rajiha 
				refused this offer too as she was not interested in leaving the 
				country. In 1999, she went to Cairo University to do her PhD; 
				but later she changed her mind and enrolled in Exeter University 
				in 2000 from where she completed her PhD in ‘Development of 
				Oman’ in 2004 Later, in the 1990s she was appointed as the 
				Undersecretary for Statistics and Information Affairs and went 
				on to become the Minister of Tourism in 2004. When the 
				announcement was made, she was attending a meeting in Beirut. My 
				son was in Britain; our two daughters were in the Netherlands 
				and US and I was alone in Oman. She rang me up from Beirut 
				asking me to tune in radio to hear an important news regarding 
				her. When the announcement regarding her appointment as the 
				minister was made, I informed her and our children abroad. We 
				were in seventh heaven.<br>
				<br>
				Even before becoming the Minister of Tourism, she used to share 
				with me her dreams for Oman’s tourism development. She would 
				tell me that the arrival of just 1,000 visitors will generate 10 
				job opportunities in the country. Rajiha who had a great vision 
				for national development had contributed greatly to drafting the 
				Vision 2020. After becoming Minister, she worked hard to achieve 
				this goal. Now the contribution of tourism sector to the 
				national GDP has grown up to 3 per cent, while the target to be 
				achieved by 2020 was only 2 per cent. <br>
				<br>
				From the day I proposed to her, I understood that she was very 
				intelligent and capable of doing a lot of things for the 
				country. I would encourage her and work with her. She was very 
				self-confident and hardworking not only as a successful 
				professional and committed civil servant but as a caring mother 
				and wife.<br>
				<br>
				<b>Faisal Murtadha Al Lawati:</b><br>
				<br>
				She was diagnosed with cancer one year ago. But it was in the 
				initial stages and she underwent treatment and was cured 
				completely. Later, when we went to Britain, she was urged to 
				undergo further tests. Though her health was sounding fine 
				according to those tests, the doctor advised her some more 
				advanced tests. To our dismay, we discovered that the disease 
				had already spread everywhere.<br>
				<br>
				But it did not upset her at all and she remained calm on hearing 
				the news. When she came back to Oman, I asked her about the 
				results of the tests. She said it had spread to various parts of 
				the body. But I could not believe what I was hearing because my 
				mother was so calm that I could not see any trace of anguish on 
				her face. I still remember what she told her doctor when she 
				went to Germany for advanced treatment. She categorically said 
				that she would not accept any treatment that would not allow her 
				to do her work. However, owing to the pressure from the family, 
				my mother conceded to get herself admitted and treated in a 
				German hospital. The treatment continued in Royal Hospital in 
				Oman under the supervision of the German hospital. Some 28 days 
				before she breathed her last, my mother became very tired and 
				breathed with great difficulty. Later, she underwent artificial 
				respiration and radiation. She had Pleurisy also and one of her 
				lungs was completely infected while the other one was affected 
				almost 90 per cent. The disease had completely taken over her 
				body. But even though my mother was admitted in the hospital, 
				every morning she would call me up and tell me to bring her work 
				to the hospital. Even till the last moment before she died, she 
				was working.<br>
				<br>
				She was more than a mother to us, she was our friend and mentor 
				and always wanted us to be successful in our life. When my 
				sister, who was studying in the US as a doctor, wanted to come 
				and take care of her, she totally rejected the idea saying, “You 
				will not succeed in life by sitting beside me; my happiness is 
				to see you scale new heights in your academic life”. <br>
				<br>
				<b>As told to Visvas <i>Paul D Karra</i></b> and <b> <i>Myssaa Al Hinai</i></b></font></p>
