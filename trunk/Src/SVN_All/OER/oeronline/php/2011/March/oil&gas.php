		        <td valign="top" width="100%"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Steady and 
					Sustaining<br>
                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2">
					<img border="0" src="images/oil&gas-1.jpg" width="150" height="153" align="left" style="border: 1px solid #C0C0C0">Petroleum 
					Development Oman is facing complexities like never before, 
					with ageing oilfields, complex projects, burgeoning demand 
					for gas and spiralling prices of commodities. Despite 
					challenges, it seems certain that the company will sustain 
					and gradually increase production in the years to come. 
					</b><i>Sunil Fernandes</i><b> reports on the reasons for such optimism</b></font><font face="Verdana" size="1"><br>
                    <br>
                    <br>
                    Taking challenges head-on has never been a problem at 
					Petroleum Development Oman (PDO). When faced with criticism 
					for declining production a few years ago, the company not 
					only reversed declining production, but managed to sustain 
					and increase production.<br>
					<br>
					The year 2010, like 2009, was a year of steady performance 
					in terms of production at PDO. The company reported, for the 
					fifth successive year, a combined production of oil, gas, 
					liquid petroleum gases and condensates at more than one 
					million barrels of oil equivalent per day. <br>
					<br>
					But if we consider the production of oil individually, there 
					was a marginal increase in production. The average oil 
					production in 2010 stood at 553,000 barrels per day, 
					slightly above the 2009 level of 552,000 barrels per day. 
					Pertinently, if the Harweel project had to go on stream as 
					anticipated by mid-2010, the company would perhaps have 
					shown a reasonable jump in production.<br>
					<br>
					<br>
					</font><font face="Verdana" size="2" color="#000080">
					<b>The big leap</b></font><font face="Verdana" size="1"><br>
					<br>
					The last few years have seen a steady oil and gas production 
					trend at PDO. Going forward, the company is faced with 
					ageing oilfields which poses a challenge in replenishing its 
					oil production. However, Enhanced Oil Recovery (EOR) 
					projects that are slated to go on stream will add almost a 
					third to production in the coming years, making it the next 
					big leg in the production story. EOR projects at Harweel, 
					Marmul, Qarn Alam and Amal will between them add staggering 
					levels to oil produced, when all of the projects go on 
					stream at full production. However, EOR projects come at a 
					cost, and at a cost that is significantly higher when 
					compared to conventional means of oil production.<br>
					<br>
					“We would be satisfied even if oil would be around $30. The 
					EOR projects at Harweel, Marmul, Qarn Alam and Amal, would 
					not have any problems with lower oil prices. Having said 
					that, if we consider today’s oil prices, these projects are 
					extremely attractive,” says Raoul Restucci, managing 
					director of PDO.<br>
					<br>
					Interestingly, PDO would be the first company to 
					simultaneously use all the three EOR techniques of polymer 
					flooding, miscible gas and steam injection, when some of its 
					projects are commissioned.<br>
					<br>
					The first of the EOR projects at Marmul has already 
					kicked-off with the plant inaugurated in October 2010. The 
					lowest in terms of oil production contribution, the Marmul 
					polymer project will add 10,000 barrels a day of incremental 
					production to PDO over the coming years. Marmul is a 
					long-term project in which increased production can take 
					many months to take effect. However, early operations have 
					been promising with increased production.<br>
					<br>
					Haweel is yet another complex EOR project, but this time 
					using miscible gas to draw-out oil. Technological challenges 
					have led to some delays, but commissioning activities at 
					this project are now underway, and first oil is expected in 
					the second quarter 2011. Once Harweel reaches full 
					production, it will contribute with an additional 40,000 
					barrels per day of high quality crude oil to PDO’s expanding 
					portfolio.<br>
					The Qarn Alam steam-injection project has also experienced 
					some delays, but the project team is working hard to recover 
					lost time. First production and first steam stages are 
					expected in August and October 2011, respectively, after 
					which it will also contribute another 40,000 barrels per 
					day.<br>
					<br>
					PDO’s newest EOR project at Amal is progressing on schedule. 
					This is a twin field development at Amal East and Amal West 
					where steam will be used in different ways to increase 
					production. Amal is expected to take several years to reach 
					its full effect with peak production at the two Amal fields 
					not expected until 2018 when output is projected to reach 
					23,000 barrels per day.<br>
					<br>
					</font><font face="Verdana" size="2" color="#000080"><b>
					Discoveries remain crucial</b></font><font face="Verdana" size="1"><br>
					<br>
					Discoveries remain crucial to ensure that depleting reserves 
					are replenished and a reserve-replacement ratio in excess of 
					one is achieved. “At the moment we delivered in excess of 
					one and we continue to work on that,” says Restucci. <br>
					<br>
					And the reasons for maintaining a healthy ratio is PDO’s 
					heavy investment in its exploration programme which has 
					resulted in significant successes. In 2010, the company made 
					a discovery of four new oil fields, one of them with 
					material volume of oil in place. The company also found a 
					potentially large gas field. Combined potential in-place 
					volumes of oil and gas could be in excess of 800 million 
					barrels of oil equivalent.<br>
					One of the material oil discoveries in 2010 was at Amal 
					Southeast, close to the existing Amal and Amal East oil 
					fields which are currently part of an important Enhanced Oil 
					Recovery (EOR) development project. In addition to this, PDO 
					has made three other oil discoveries at Sayyah in the north 
					of PDO’s concession, Al Ghubar East and Aqeeq in the central 
					region of the Sultanate. <br>
					<br>
					“Now there is lot of work, like appraisal, testing and 
					determining the full commerciality of these projects. But we 
					are very excited about these projects,” says Restucci.<br>
					<br>
					</font><font face="Verdana" size="2" color="#000080"><b>An 
					intensive gas programme</b></font><font face="Verdana" size="1">
					<br>
					<br>
					The Sultanate is witnessing a rising demand for natural gas 
					to drive industrial growth and the demand-supply scenario 
					continues to remain tight. “We continue with an intensive 
					gas exploration programme, including development and 
					seismic. But in addition to that we have gone significantly 
					into tight and unconventional gas,” says Restucci.<br>
					<br>
					The search for new gas is focused on deep tight gas 
					exploration throughout Oman. In northern Oman, up to 30 
					tight gas wells will be drilled and tested over the next 
					four years and extensive 3D seismic data will be acquired. 
					In southern and central Oman, PDO plans to drill new wells 
					to test new gas play concepts, it will acquire additional 3D 
					seismic data, and carry out in-depth geological studies. <br>
					Already in 2010, three new gas fields at Burhaan West, 
					Mabrouk and Harmal were brought onstream on schedule, giving 
					an important boost to PDO’s ability to sustain gas 
					production over the coming years. And new natural gas 
					discoveries continue. In 2009 it was Khulud South and in 
					2010 Khulud West. Clearly, the gas story at PDO remains 
					huge, and the company is intensifying efforts to ensure that 
					production is increased and then sustained.<br>
					<br>
					</font><font face="Verdana" size="2" color="#000080"><b>
					Reasons for optimism</b></font><font face="Verdana" size="1">
					<br>
					<br>
					There is little doubt that PDO is facing challenges from 
					ageing fields, cost increases, growing demand for gas and 
					increased complexity of its projects. But each of these 
					challenges seemed to be well mitigated by new discoveries, 
					EOR projects slated to go on stream, sustaining natural gas 
					supplies and improving operating efficiencies. <br>
					<br>
					“Clearly, the response will be by sustaining production on 
					the exploration front, building development activity, 
					commissioning EOR expertise, strengthening efforts in 
					managing costs, driving efforts in energy consumption for 
					EOR projects, strengthening of project engineering service. 
					It’s about operational excellence, technology and our 
					people,” says Restucci.</font></p>
