					</font></b><font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><b>A prominent player 
					in various development projects in the interior and 
					far-flung wilayats of the Sultanate, Hasan Juma Bakcer has 
					significantly contributed to the inclusive growth and 
					infrastructure development of the country in the last 35 
					years, says  
                    </b><i>Krishnakumar Taori</i><b>, Group Managing Director, in 
					an interview with 
                    </b><i>Muhammed Nafie</i><b>.<br>
					<br>
                    </b></font><font face="Verdana" size="1"><br>
                    <b>
                    <img border="0" src="images/CP-1.jpg" align="left" hspace="12" vspace="6" width="150" height="179" style="border: 2px solid #C0C0C0">Please 
					give a brief on Hasan Juma Backer and its operation in Oman?</b><br>
					Hasan Juma Backer (HJB) started its operation in 1975. Over 
					the last 35 years, the company has emerged as one of the 
					leading civil electro-mechanical construction and project 
					management companies in the country. We are working on 
					various infrastructure projects and the major areas of our 
					operations include buildings, roads, substations, pumping 
					stations, instrumentation, fire fighting, sewage networks, 
					industrial infrastructure projects, schools, colleges, 
					hospitals and leisure and sports projects. HJB has provided 
					civil, electro mechanical and construction services to a 
					wide spectrum of clients including the Diwan of Royal Court, 
					Royal Oman Police, Royal Guard of Oman, Public Establishment 
					for Industrial Estates, Ministry of Defence and other 
					ministries and important establishments in public and 
					private sectors.<br>
					<br>
					<b>Can you give an overview of various projects that the 
					company is executing presently?<br>
					</b><br>
					We have been recently awarded a 190-villa project of over 
					RO16mn by Muscat municipality in Yeti. This is one of the 
					prestigious projects that we are currently executing. In 
					addition, we are working on the extension of the ministry of 
					foreign affairs building and doing various construction 
					works for the Royal Guard of Oman and a number of water 
					supply projects in Al Rustaq, Al Awabi and Al Musanah areas. 
					Besides, our Qatar operation is very active at the moment. 
					In Qatar we are working for Kahramaa as well as Qatar 
					Petroleum. We have recently got some important projects from 
					Qatar Petroleum.<br>
					<br>
					Last year, we completed the infrastructure developments of 
					the third, fourth, fifth and sixth phases of Sohar 
					Industrial Estate which involve road works, 
					electro-mechanical services, irrigation, fire-fighting, 
					water supply and landscaping. In short, we have completed 
					the entire facilities required for the companies such as 
					aluminum smelting plant and various other industries to 
					start operation. In 2010, we also did the civil works for 
					helicopter facilities for the ministry of defence and water 
					transmission project from Nizwa to Bahla, rehabilitation of 
					Muttrah Internal roads (Muscat Restoration) and Mudaybi 
					Internal roads in A’sharqiya Region.<br>
					<br>
					<b>What was the contribution of HJB to the economic growth 
					and development of Oman in the last three and half decades?<br>
					</b><br>
					Over the last 35 years, HJB has grown from a small company 
					of 50 employees in 1975 to the strength of 3,500 employees 
					in 2010. We have significant employment opportunities for 
					Omanis and have contributed to the development of Oman. As a 
					pioneer in infrastructure development in the country, HJB 
					has executed various power, water, health, leisure and 
					sports projects across the country. Our major civil 
					electro-mechanical construction projects include land mark 
					buildings of RGO, OmanGas, Oman LNG, State General Reserve 
					Fund, College of Banking and Financial Services etc., 
					hospitals such as Wadi Heibi, 100-bed extension to Jalan 
					Bani Bu Ali hospital, university colleges and schools in 
					Rustaq and Ibra, several buildings in Sultan Qaboos 
					University and the ministry of education’s schools.<br>
					<br>
					HJB has executed various development projects and 
					infrastructure facilities in the interior areas of the 
					country. We have set up several water pumping stations and 
					pipelines across the country. Our presence is ubiquitous in 
					almost all interior and far-flung Wilayats of the country 
					such as Musanna, Awabi, Rustaq, Wadi Bani Khalid, Nakhl, 
					Buraimi and Khasab. To carry out our projects, we partnered 
					mainly with local contractors and subcontractors from the 
					respective areas so that we can generate a lot of employment 
					opportunities for the nationals. A green card holder for 
					Omanisation for many years, HJB continues to hold training 
					programmes for its national work force. To cap it all, we 
					have a strong base of the equipment and resources which 
					ensure the quality, excellence and effectiveness of our 
					projects.<br>
					<br>
					<b>With Vision 2020, the government is focusing on 
					diversifying the economy. What is your contribution to this?</b><br>
					<br>
					We have our own plans for diversification. We have already 
					ventured into hospitality sector and are looking to execute 
					various tourism related projects. We have also built 
					schools, colleges and hospitals. Apart from the hospitals in 
					Wadi Heibi and Jalan Bani Bu Ali , we have set up renal 
					dialysis centres in Sohar and various interior areas such as 
					Ibra, Ibri, Nizwa, Khasab, Buraimi and dialysis centre in 
					Nizwa, Bahlah and other areas. Besides, we have set up 
					nursing training schools in all the six regions for the 
					ministry of health.</font></p>
