					<b>What are the major challenges facing the construction 
					industry in the country?</b><br>
					<br>
					There are different challenges in different areas. Manpower 
					shortage and commodity prices are the two likely big 
					challenges in 2011. The rise of borrowing cost from banks is 
					another challenge that the construction industry is going to 
					face. Increasing interest rate is a big challenge all over 
					the world. The market is competitive because of new players, 
					especially foreign companies. Registration procedures are 
					very easy for foreign companies in Oman compared to other 
					GCC countries. Omani companies will not get that easy access 
					and similar treatment outside in other GCC countries.<br>
					<br>
					<b>What about your foreign operations?</b><br>
					<br>
					HJB has gone international by taking up major water projects 
					in Qatar with Qatar General Electricity and Water 
					Cooperation after successfully completing a 
					techno-commercial evaluation on its own strength against 
					international companies. The company seeks to expand its 
					presence in Doha. And with Qatar winning FIFA World Cup bid 
					for 2022 and since the government is looking to do a lot of 
					infrastructure projects in the country, we expect to play a 
					bigger role.<br>
					<br>
					<b>A word about your civil and electro-mechanic divisions?</b><br>
					<br>
					The civil division of HJB, which is also the traditional 
					strength of the company, has expanded manifold over the past 
					30 years with several major works to its credit. The 
					division is currently fully equipped to execute large-scale 
					turn-key projects. The electro mechanical division of the 
					company played an important role in executing projects in 
					very far-flung areas and contributes its might to the 
					universal electrification and drinking water distribution. 
					We do all integrated works. We have electrical mechanical 
					instrumentation and scada instrumentation technology which 
					help us build mega pumping stations with 400-500 KW 
					automatic motors.<br>
					<br>
					<b>The government is investing heavily in ports and free 
					zones. What opportunities do you see there?</b> <br>
					<br>
					We will be definitely involved in all these expansion 
					projects. We have tremendous experience in this area. We 
					have been a seasoned player and have a big role to play in 
					the development of infrastructure facilities for free zones 
					across the country. </font></p>
