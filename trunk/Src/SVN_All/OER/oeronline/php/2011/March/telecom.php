                  <p align="justify" style="margin: 3px 10px"> 
					<font size="1"><font face="Verdana"><b>Rural success</b><br>
					<br>
					One of the major aspects that contributed towards the 
					substantial growth of Micromax is its 80 per cent sales in 
					the rural areas. In fact, it was in one such rural settings 
					that the idea for a Micromax mobile handset was born which 
					until then was delivering technology of fixed 
					wireless-powering desired products to companies.<br>
					<br>
					The year was 2007, when executives of Micromax noticed a 
					curious sight in one particular village, in eastern India. 
					People lined up next to a man who had a car battery strapped 
					to the back of his bicycle. They were charging their mobile 
					handset batteries by paying a few rupees as their homes 
					didn’t have electricity.<br>
					<br>
					Realising this great need, less than a year later, Micromax 
					sold its first cell phone, the X1i, with an oversized 
					battery, a small screen, and tweaked electronics that made 
					the phone run for as long as five days, and on standby for 
					as many as 30 days. And the rest as they was history.<br>
					<br>
					And that came about purely by listening to customers’ 
					feedback.</font></font></p>
