                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Successful 
					innovation<br>
                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><b>Micromax, which is 
					launching its mobile phones in Oman soon, is looking at 
					repeating its success story in the Sultanate, thanks to its 
					customer-focussed products. </b><i>Vikas Kumar</i><b>, VP Middle East &amp; 
					Africa speaks to Visvas Paul D Karra about the company’s 
					philosophy</b></font><font face="Verdana" size="1"><br>
                    <br>
                    <img border="0" src="images/telcome-1.jpg" align="left" hspace="12" width="150" height="205" style="border: 2px solid #C0C0C0">Businesses 
					succeed or fail based on whether they have been listening to 
					their customers. This is more so in the case of mobile 
					telephony market. A case in point is Micromax Informatics 
					Limited, the Indian mobile handset manufacturing company, 
					which witnessed a meteoric rise thanks to its 
					customer-centric products giving established international 
					mobile phone brands a run for their money.<br>
					<br>
					Just to give an indication of the popularity of the brand, 
					Micromax sold 7.05 million mobile handsets in fiscal 2010. 
					The handset sale has grown by 132.43 per cent from 1.11 
					million units in the quarter ended in June 2009 to 2.58 
					million units in the quarter ended on March 31, 2010. 
					Micromax also sells mobile data cards for computers to 
					service providers in India.<br>
					<br>
					“Product innovation has been a key factor in driving 
					Micromax growth as our strategy is to provide unique value 
					propositions to our customers in terms of innovation in 
					quality and pricing. This automatically attracts customers 
					to us,” says Vikas Kumar, VP, Middle East and Africa, 
					Micromax.<br>
					<br>
					Micromax handsets will be aimed at consumers across all 
					segments with special focus on the age group of less than 30 
					years because it offers a wide variety of phones that range 
					from gaming specific phones all the way to social media that 
					are in line with its brand essence “Nothing like anything”. 
					“We are also eyeing the expatriate population from the 
					sub-continent as they already have an experience with our 
					products in India,” says Kumar who was on a short visit to 
					Oman in February.<br>
					<br>
					Micromax has announced its plan to launch its phones in Oman 
					by entering into a distribution agreement with Molecules 
					Group of Companies. The launch date was not announced at the 
					time of OER going to print as Molecules is still awaiting 
					the final approvals from Telecom Regulatory Authority of 
					Oman.<br>
					<br>
                    <br>
                    <b>Entire value chain<br>
					</b><br>
                    However, what is of interest for the Omani consumer is the 
					fact that Micromax has the entire value chain of handsets, 
					with phone features ranging from 30-day long battery life 
					(standby mode), dual SIM, QWERTY phones, multimedia and 
					utility phones, points out Kumar, who has plenty of 
					experience in the mobile phone market. Kumar was with 
					Samsung in Africa before joining Micromax in the Middle 
					East.<br>
					<br>
					Following its success in India, Micromax started selling in 
					Nepal, Sri Lanka, Bangladesh and the UAE. Besides Oman, it 
					has also forayed into Kuwait and Qatar as well through its 
					wholly owned subsidiary, Micromax Informatics FZE with local 
					partners.<br>
					<br>
					Kumar says that Micromax is privileged to have consumer 
					confidence, which has enabled the company to establish 
					itself in India, in a short period. According to him, the 
					Omani market is very exciting and he believes that consumers 
					would appreciate the dual SIM handsets, which would allow 
					them to carry two mobile numbers in one phone.<br>
					<br>
					Talking about market segments, Kumar believes that Micromax 
					will be able to replicate its growth in the region through 
					its target segment with varied range of products. He says 
					that they will be able to set up a new segment with each of 
					its products in the new markets.<br>
					<br>
                    <br>
                    <b>Visible brand<br>
					</b><br>
                    The aim of Micromax is to become an established brand in all 
					the market territories where it operates by positioning 
					itself in such a way so as to enhance its brand visibility.<br>
					<br>
					Therefore, the marketing strategy of Micromax is to focus on 
					the unique functionalities of the phone products, which 
					further strengthens its reputation for innovation.<br>
					<br>
					Talking about competition, Kumar says his company 
					differentiates itself through innovation and design, the use 
					of advanced technologies and an in-depth understanding of 
					rapidly changing consumer preferences. This has enabled 
					Micromax to develop several new product categories that 
					address unique customer needs and further, its product 
					development capabilities have helped it to establish itself 
					as an innovative Indian mobile handset company.<br>
					<br>
					Since its entry into the Indian mobile handset market in 
					March 2008, Micromax has introduced more than 40 distinct 
					mobile handset models and as of August 31, 2010, the company 
					sold more than 30 distinct mobile handset models.<br>
					<br>
					Micromax has many ‘firsts’ to its credit thanks to its 
					versatile product portfolio. It was the first to introduce: 
					Handsets with 30 days battery backup, handsets with dual SIM/ 
					dual standby, handsets switching networks (GSM - CDMA) using 
					gravity sensors, aspirational Qwerty keypad handsets, 
					operator branded 3G Handsets, OMH CDMA Handsets, etc.<br>
					<b><br>
					<br>
					Out-of-the-box solutions<br>
					</b><br>
                    No wonder then that, today, Micromax has become a brand, 
					which people relate and look up to for realising their 
					individual device preferences and other out-of-the-box 
					solutions.<br>
					<br>
					Micromax has introduced 37 phones in just over a year and a 
					half, designing them in India and manufacturing with 
					partners in China. The company has kept its phones at an 
					affordable price and tailored to local tastes.<br>
					Most Micromax handsets care less about Wi-Fi, 3G, or GPS 
					capabilities, for example. That keeps costs down. One phone 
					doubles as a Nintendo Wii-like controller, allowing users to 
					play games on a television game console. Another, marketed 
					heavily with Bollywood-themed TV commercials, has costume 
					jewellery embedded in it and swivels open to reveal a full 
					keyboard. <br>
					<br>
					Micromax is selling about one million handsets each month, 
					or about 4 percent of the $6.3bn Indian market; Indian phone 
					makers as a group have grabbed 14 percent of the market, 
					according to research by Indian trade magazine Voice&amp;Data.<br>
					<br>
					Micromax, which is also planning an IPO, was approached by 
					Boston-based TA Associates, a $16bn private equity fund that 
					invested $45mn in the company in January for an undisclosed 
					stake. The company plans to raise a little over INR4.26bn 
					from the capital market with a public issue of 20 million 
					shares. This values the company, which entered the mobile 
					handset business only three years ago, at close to $1bn.<br>
					<br>
					The Gurgaon-based mobile handset company plans to set up a 
					research and development centre in China by the year-end at 
					an investment of INR500mn.<br>
