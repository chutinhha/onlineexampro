					</font></b><font face="Verdana" size="1"><br>
					Already well-known for its high-profile investments in 
					Europe, the Middle East, and Asia, Qatar Holding made a 
					splash when it announced in October 2010 that it would 
					acquire a five per cent stake in banking giant Santander’s 
					Brazil operations, making it the second-largest stakeholder 
					in the Latin American bank. The move lent credence to the 
					country’s ambitions to be a truly global financial player, 
					and set it up well to access further Latin American markets. 
					It was also among Qatar’s most value-conscious investments 
					to date, with the bonds Qatar Holding bought to convert into 
					shares exchanging at QAR23.75 a share, at the time below 
					market value. In addition, Santander’s chairman reported 
					that the bank expects higher profit from Brazil than Spain 
					this year in October. To those who had doubts, this key 
					acquisition proved that Qatar isn’t treating its investments 
					lightly. </font></p>
