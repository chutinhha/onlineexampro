					</font>
					</b><font face="Verdana" size="1">The days of Qatar being an 
					“undiscovered” market are over. While the country has been 
					punching far above its weight on the international economic 
					and diplomatic scene for years, its ascendancy to 
					household-name status throughout the world has undoubtedly 
					come from its successful bid to host the 2022 FIFA World 
					Cup.<br>
					<br>
					This may seem incongruous for a nation with few natural 
					patches of grass or one where daytime temperatures at the 
					time of the tournament are known to touch 50 degrees 
					centigrade. However, the vision of the Qatari leadership has 
					managed to convince the FIFA leadership that these 
					challenges in no way impede its ability to be a stellar 
					host. <br>
					<br>
					This isn’t news to everyone: long before the 
					much-anticipated announcement in December, Qatar had been 
					racking up a list of top international events. Bold and 
					ambitious, these events have turned out to be extremely 
					successful business ventures. People outside the 
					region/industry are unlikely to have noted that Qatar became 
					the world’s largest producer (not just exporter, as it was 
					formerly) of liquefied natural gas (LNG) in 2010.<br>
					<br>
					Some may have noticed the country’s media presence – both as 
					the base of news network Al Jazeera and, through the Qatar 
					Foundation, the sponsor of the BBC’s “Doha Debates,” 
					described on their website as “a unique venture in the Arab 
					world, providing a battleground for conflicting opinions and 
					arguments about the major political topics of the region.”<br>
