					roster. Few in the luxury retail business needed another 
					reminder that Qataris like to shop – but those unacquainted 
					with the Qatari market reacted with a mix of befuddlement 
					and curiosity.<br>
					<br>
					It takes a delicate blend of exclusivity, innovation and 
					buzz to become a destination on the international luxury 
					shopping circuit. With such a high-profile move into the 
					market in London, Qatar has certainly managed to attract the 
					attention of this high-margin set, and may consequently see 
					more people coming to explore retail options in Doha. The 
					city certainly looks set to capitalise on this interest, 
					with gross leasable area (GLA) of retail space set to double 
					to 400,000 sq metres over the coming years.<br>
					<br>
					<br>
					</font><b><font face="Verdana" size="4" color="#C00000">
					Brazil-Argentina match<br>
					</font></b><font face="Verdana" size="1"><br>
					Held just before the 2022 bid results were announced, this 
					game is widely credited with clinching the host position for 
					Qatar. The timing could have hardly been better: the 
					legendary South American rivals had been tied in a dead heat 
					of 33 wins each over their last 89 encounters. The 
					successful accommodation of 40,000 fans was touted as proof 
					that the peninsula had the potential to support the 
					estimated 400,000 fans that are expected to descend in 2022. 
					This was far from the first high-profile match played in 
					Qatar – November 2009’s England-Brazil stands out – but it 
					was the most widely watched sporting event to date, airing 
					on ESPN worldwide and giving many football lovers their 
					first glimpse of the country.<br>
&nbsp;</font></p>
