                    </font><b><font face="Verdana" size="4" color="#C00000">
				Thinking beyond<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="2"><b>Freezone Sohar is expected to 
				give a fillip to the development of Sohar region in tandem with 
				Port Sohar and Sohar Industrial Area thanks to a strategic plan 
				for cluster development. Visvas Paul D Karra takes stock of the 
				recent developments in this special economic zone</b></font><b><font face="Verdana" size="4" color="#C00000"><br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px" dir="ltr">
				<img border="0" src="images/infrastrucgture-1.jpg" width="450" height="300" style="border: 1px solid #C0C0C0"><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				<br>
                When the government decided to develop Sohar as the industrial 
				hub of Oman and reclaim the region’s past status as the key port 
				in the Arabian Sea, little did they realise that it would 
				literally be a date with history.<br>
				<br>
				A joint excavation carried out by the ministry of heritage and 
				Sohar Free Zone management team, in the area designated for the 
				special economic zone, unearthed tombs dating back to 1,000BC. 
				The team found human skulls and bones, horse bones, teeth, 
				pieces of glass pots, gold and silver beads, agate stone, 
				shells, earrings, bronze and iron rings in 57 tombs that date 
				back to 1,000BC and the beginning of 2,000BC during the era of 
				Wadi Al Shouq of the Iron Era (2,000/300BC). This was the period 
				when business was booming in Sohar, the gateway for trading 
				important commodities including metals like copper and iron and 
				food items such as dates and honey.<br>
				It seems to be a remarkable coincidence that this important 
				archaeological discovery was made in the area earmarked for the 
				Sohar Free Zone development, a 45 sq km-land mandated by the 
				government to propel Sohar’s reputation as the modern trading 
				hub of the region in combination with Port Sohar and Sohar 
				Industrial Area.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Royal 
				Decree</b></font><font face="Verdana" size="1"><br>
				<br>
				The development of the Sohar Free Zone, now branded as Freezone 
				Sohar, got a strong impetus following the promulgation of the 
				Royal Decree 123/2010 issued in September 2010. The long-term 
				strategy crafted for this region was to have industrial 
				development which can generate economic spinoffs for the local 
				population in particular and the country in general.<br>
				<br>
				The Royal Decree provides numerous benefits to international, 
				regional and local investors at Freezone Sohar in addition to 
				the unique strategic, geographical and regulatory advantages, 
				which are available in this special economic zone.<br>
				<br>
				Investor interest in the 4,500-hectare scheme has been strong 
				from the outset thanks to the proven industrial performance, 
				according to officials of Sohar Free Zone, spearheaded by Sohar 
				Industrial Development Company, a joint venture between the 
				Government of Oman, the Port of Rotterdam and SKIL 
				Infrastructure in India.<br>
				<br>
				It may be noted that during the 7th Five Year Plan (2006-2010), 
				sizeable investments were poured in to major sectors of the 
				manufacturing industries, such as petrochemicals, heavy 
				industries, (steel, iron, aluminium, fertilisers) as well as 
				small and medium industries which form a strong base for the 
				manufacturing industries in the Sultanate. The industry sector 
				witnessed remarkable growth in terms of added value, GDP, 
				exports, increase in the number of industrial projects and 
				Omanisation.<br>
				<br>
				During the 7th plan, the growth rate of the manufacturing 
				industry amounted to 18.4 per cent compared to 23.2 per cent of 
				refined oil products. Basic chemical materials achieved 20.2 per 
				cent growth rate. These rates constitute the highest growth in 
				different sectors.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Batinah 
				gains importance</b></font><font face="Verdana" size="1"><br>
				<br>
				HE Maqbool bin Ali Sultan, Minister of Commerce and Industry, 
				says that Sohar is an important location because the Batinah 
				region has the highest concentration of the Omani population. 
				Apart from investments worth around $14bn which have poured in 
				to industrial port of Sohar, for various industries including 
				aluminium, propylene, fertiliser, aromatics, steel etc., the 
				idea now is to attract a number of small and medium enterprises 
				and industries into Freezone Sohar. These industries will become 
				the downstream units for the big-ticket industries in the Sohar 
				Port. This will in turn generate more employment for the local 
				population, says HE Maqbool.<br>
				<br>
				The Freezone Sohar, together with the Sohar Industrial Area and 
				Port Sohar, forms a massive industrial zone where the private 
				sector has a big role. The multi-nodal transport infrastructure 
				like the roads (Muscat Expressway, Coastal Highway and the 
				existing highway) linking Sohar with Muscat, as well as the 
				proposed railway and airport will make Sohar a major port for 
				goods coming in to and going out of Oman.<br>
				What is important, as far as the population of Batinah is 
				concerned, is the education and training potential, says HE 
				Maqbool. “We have the International Maritime College in addition 
				to the Sohar University, as well as other technical and 
				vocational colleges. This will take care of the human resources 
				by employing the people in the free zone. Once the industries 
				come up, it will not only help the existing companies but other 
				ancillary services like hotels and transport companies will be 
				generated.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Steel 
				cluster</b></font><font face="Verdana" size="1"><br>
				<br>
				As a special economic zone located adjacent to the highly 
				successful Port Sohar, Freezone Sohar has paved the way for a 
				number of dedicated clusters at the 4,500-hectare free zone. The 
				first phase, covering a total leasable area of around 350 
				hectares, will have clusters related to logistics and 
				warehousing (200 hectares); light manufacturing (100 hectares); 
				and industry (150 hectares). Some seven companies have already 
				obtained leases to set up operations as part of the phase 1 
				development of the scheme.<br>
				<br>
				A downstream steel cluster, for the steel projects in operation 
				and under development at Port Sohar, is being pursued by 
				Freezone Sohar in collaboration with the state-owned energy 
				investment vehicle, Oman Oil Company. <br>
				<br>
				The proposed steel cluster represents a unique value proposition 
				to investors, given especially Freezone Sohar’s proximity to a 
				slew of iron and steel ventures like the newly launched steel 
				mill of Jindal-Shadeed, and the soon-to-be-operational $1.5bn 
				iron ore pelletising scheme of Vale Oman, says Jamal T Aziz, CEO 
				of Sohar Free Zone and deputy CEO of the Port of Sohar.<br>
				<br>
				Jindal-Shadeed’s fully integrated steel complex at Port Sohar, 
				due to commence operations soon, is a Direct Reduction Iron (DRI) 
				plant with a capacity to produce 1.5 million tonnes of hot 
				briquetted iron (HBI) per annum. Brazilian mining conglomerate’s 
				local subsidiary Vale Oman is also close to completing work on 
				its giant $1.35bn pelletising plant capable of producing 9 
				million metric tonnes of DRI pellets annually.<br>
				<br>
				Both ventures, according to Jamal, have the potential to create 
				synergies that make downstream steel-related investment an 
				attractive proposition for local, regional and international 
				investors.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Future 
				developments</b></font><font face="Verdana" size="1"><br>
				<br>
				Freezone Sohar is a dynamic development and its most unique 
				value proposition is the logistics advantage that investors can 
				leverage to access markets in the Gulf, Indian sub-continent and 
				the wider Middle East region. Excellent connectivity afforded by 
				a world-class seaport and road network – and rail and air access 
				in the future – further underscores the free zone’s investment 
				appeal. <br>
				<br>
				Neelima Vyas, COO of Sohar Free Zone, says that companies from 
				Gulf Co-operation Council (GCC), Indian sub-continent and Europe 
				have already signed up for setting up industries within the free 
				zone in the first phase. The projects involve capital 
				investments in excess of $60mn, Vyas adds while saying that the 
				first company within the free zone is expected to start 
				operation by the third or fourth quarter of next year.<br>
				<br>
				One of these is Zubair Furnishing, a global contract furnishing 
				company wholly owned by The Zubair Corporation, which has signed 
				a Letter of Intent (LoI) with Sohar Free Zone as a precursor to 
				negotiate the terms of a land lease agreement for a four hectare 
				plot within Freezone Sohar.<br>
				<br>
				Also on the anvil is a dedicated cluster earmarked exclusively 
				for educational and medical services for which an area of 50-60 
				hectares will be allocated. A major Grain and Dry Bulk Foods 
				Terminal being planned at the Port of Sohar would stimulate 
				investments in food related industries at the free zone. The 
				terminal, whose scope and capacity is still the subject of 
				evaluation, is likely to be built on a reclaimed plot designated 
				as part of the Future Container Terminal in Port Sohar and has 
				been conceived in line with the government’s food <br>
				security strategy.</font><p align="justify" style="margin: 3px 10px" dir="ltr">
