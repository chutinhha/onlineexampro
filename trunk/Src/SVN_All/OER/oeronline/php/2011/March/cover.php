					<b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Tourism-driven growth<br>
					</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					Another trend giving impetus to ITCs is the government’s 
					tourism thrust. The Vision 2020 plan aims to promote tourism 
					as a priority sector of Oman’s economy. In 2009, tourism 
					contributed 6.7 per cent of GDP. By 2019, this is estimated 
					to reach 9.9 per cent of GDP. Things seem to be moving in 
					the right direction. Though the tourism sector showed a 
					year-on-year (y-o-y) decline of 8.3 per cent in 2009, the 
					World Travel and Tourism Council (WTTC) expects Oman’s 
					tourist sector to have the highest growth in the Middle East 
					and the third-highest growth in the world in 2010. Real GDP 
					growth for the tourism sector is expected to be 6.1 per cent 
					in 2010 and to average 5.6 per cent per annum over the 
					coming 10 years.<br>
					<br>
					The growth of tourism is expected to fuel a demand for 
					hotels, retail, entertainment complexes and these augur well 
					for the real estate sector. According to the Ministry of 
					Tourism figures, the number of hotels and hotel rooms in the 
					Sultanate has increased steadily over recent years, with a 
					particularly strong annual growth of 11.7 per cent for both 
					hotel numbers and rooms between 2008 and 2009 following 
					relatively limited development in 2007 and 2008.<br>
					<br>
					According to Ministry of National Economy figures, Muscat 
					capital area has a total of 93 hotels as well as a number of 
					hotel apartments and guest houses providing a total of 6,103 
					rooms and 8,849 beds. In the higher quality sector, there 
					are eight five-star hotels with 1,844 rooms, ten four-star 
					hotels with 1,368 rooms and ten three-star hotels with 580 
					rooms. A vast majority of five and four star hotels came on 
					board before 2005. New development within this sector has 
					been somewhat limited over recent years other than the 
					opening of the three five - star hotels at Shangri-La’s Barr 
					Al Jissah Resort in 2006, the four-star Park Inn at Al 
					Khuwair in 2010 and the recent opening of the 85-room 
					Platinum Hotel, which is also in Al Khuwair.<br>
					<br>
					While the growth in the number of hotels and rooms has been 
					steady, hotel occupancy has been hit by the global 
					recession. Accordingly, occupancy slumped by around 10 per 
					cent in 2009, with the five-star sector suffering the 
					largest losses. Recent figures released by the Ministry of 
					National Economy indicate that occupancy rates for five and 
					four star hotels have been around 50 per cent in 2009 and 
					the first half of 2010. And though guest numbers have 
					increased by three per cent in 2010, revenues have decreased 
					by seven per cent in comparison with 2009. Says Ayjaz, “Oman 
					should promote itself as a tourism destination rather than 
					relying on product specific selling. Secondly, we need to 
					focus on global customers as Oman is well known in the GCC 
					region.” <br>
					<br>
					<img border="0" src="images/cover-4.jpg" width="150" height="161" align="right" style="border: 1px solid #C0C0C0">The research company, Proleads, reports that there are 
					currently 39 hotel developments in the pipeline in Oman. 
					There are several significant hotels either under 
					construction or in the planning stages which would more than 
					double the number of five-star hotels in the Muscat capital 
					area. The business travel sector is being catered for by the 
					development of two four-star hotels in Al Khuwair (City 
					Seasons and Shaza Hotel) and the proposed Muscat Convention 
					Centre which would include four three to five- star hotels 
					and serviced apartments.<br>
					<br>
					The long term outlook for tourism and hotels in Oman is 
					bright but with the projected supply of hotel rooms across 
					the four to five-star sector, careful consideration will 
					need to be given to the locations and target markets of 
					future hotel developments.<br>
					<br>
					A Royal Decree (76/10) issued in September has relaxed both 
					foreign shareholding restrictions and limitations on the 
					usage of land. Public and closed joint stock companies with 
					a minimum of 30 per cent Omani shareholding are now able to 
					own land and, perhaps more significantly, engage in real 
					estate development as a business objective, a permission 
					that was previously restricted to 100 per cent-Omani and 
					later 100 per cent GCC owned companies. The relaxation of 
					restrictions should provide further impetus to the real 
					estate sector.</font><p align="justify" style="margin: 3px 10px" dir="ltr">
