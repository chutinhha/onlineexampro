					<b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Office market takes a hit<br>
					</font></b>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					Limited office space availability across the market led to 
					rapidly escalating rental values in Muscat up until early 
					2008 and also triggered a boom in office space construction 
					that is now starting to feed into the market. It is 
					estimated that approximately 500,000 square metres of new 
					office space will come into the market across the capital 
					area by the end of 2013. The majority of this new 
					development is spread across the western section of Muscat; 
					but an area that is showing particularly rapid development 
					as a commercial area is the previously vacant land between 
					Ghala industrial area and Sultan Qaboos Highway. Says Hassan 
					Mohammed Juma, managing director, Mohamed Juma Sultan Co 
					(which owns Era Oman), “Oman’s office market remains 
					volatile as asking rents have declined and vacancy rates are 
					close to historic highs.”<br>
					<br>
					A combination of the recent financial crisis and an 
					increasing supply of new office space has resulted in the 
					decrease of rental values over the last two years. Office 
					space rentals which touched RO13-14 per square metre in 2008 
					have now fallen to around RO8 per square metre. Says Lora, 
					“Commercial rents in Oman were higher than class A buildings 
					in the US except that office buildings in the US gave 
					tenants ample parking space, building amenities and high 
					speed elevators while buildings in the local market leave a 
					lot to be desired.” Analysts consider that, as excess supply 
					continues to build in the Muscat office market, rental 
					values will reduce while vacancy levels rise.<br>
					<br>
					Demand for office space remains steady but the majority of 
					enquiries are for spaces of less than 1,000 square metres 
					with small, good quality<img border="0" src="images/cover-6.jpg" width="150" height="193" align="right" style="border: 1px solid #C0C0C0"> offices of less than 200 square 
					metres seeing strong demand, while demand for large spaces 
					is almost exclusively generated by government bodies. A 
					particular issue that is not successfully addressed in a 
					majority of commercial developments is the provision of 
					suitable car parking with many developments relying almost 
					entirely on off-site parking on un-developed land. Says 
					Philip Paul, head of country, Cluttons Oman, “Developers 
					tend to overbuild their plots leaving little open space, 
					while what works is probably a smaller building with 
					adequate parking space.” Commercial rents are expected to 
					head southwards with higher vacancy levels as supply starts 
					to significantly outstrip demand. Poorer quality buildings 
					with inadequate facilities will be particularly affectes.</font><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Residential property looks up</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					Unlike office space, rents in the residential property 
					market have stabilised as demand and supply are balanced. 
					Rents rose steeply from 2006-08 as demand for residential 
					dwellings outstripped supply. According to the ministry of 
					human resources, as many as 17,000 jobs were created in the 
					market every month in 2008 and the supply of houses could 
					not keep pace with demand and rents saw a steep escalation. 
					The number of jobs being created in 2010 averaged around 
					6,000 per month, and, with more houses and apartments coming 
					into the market rents have softened. Says M Sudhakar Reddy, 
					general manager, Al Habib and Company, “Rents are down 
					compared to 2008 but they are still double of what they were 
					five years ago as the economy has settled at a higher per 
					capita GDP.” The fact that 90 per cent of the real estate 
					market is residential has a huge bearing on the prevailing 
					sentiment of the market.<br>
					<br>
					Sohar’s real estate market was in for a rude shock as rental 
					and capital values plummeted sharply in the wake of 
					oversupply. A two-bedroom house which was rented for RO400 
					in 2008 is now struggling to find takers at RO150. Says Juma, 
					“The market value of land located in and around Sohar, Saham, 
					Barka and in some areas on the outskirts of Muscat have 
					declined by approximately 40 to 70 per cent from prices 
					which prevailed during the middle of 2008 depending on 
					specific characteristics such as zoning, location, size 
					etc.”</font><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Tightening of purse strings</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<img border="0" src="images/cover-7.jpg" width="150" height="155" style="border: 1px solid #C0C0C0" align="left">Banks were often blamed for creating an artificial liquidity 
					crisis in the real estate sector. As the subprime crisis 
					escalated, banks became wary of lending to realty. Says 
					Mohamed Nawaz al Balushi, head of mortgage sales, Retail 
					Banking Division, National Bank of Oman (NBO), “In the early 
					days of the crisis, disbursal of our Al Manzel home loans 
					was slow as we learnt from the mistake of others and were 
					cautious.” Overall, there was a renewed focus on quality as 
					banks started looking at metrics like debt burden ratio 
					closely. Hassan Amir Shaban, chief marketing officer, NBO 
					points out, “Oman’s real estate is geared towards Omanis 
					unlike some other countries which were dependent on 
					expatriates, prices are realistic in the local market and 
					the government’s vision is to help every Omani to have a 
					house. All these help in making the real estate sector in 
					Oman more stable.”<br>
					<br>
					Home loans disbursement has picked up in the last year or 
					so. “People have started building houses not just in the 
					capital area but also in the interiors leading to a greater 
					off-take of home loans,” adds Mohamed. Home loans are seen 
					as a way of helping the local community meet their housing 
					needs and most banks are keen to increase its disbursal. A 
					softening of interest rates from 7.75 per cent to 6.75 per 
					cent (on a variable loan) has also helped in making home 
					loans more attractive in the last one year, giving the 
					beleaguered sector much needed respite.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Silver lining</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<img border="0" src="images/cover-8.jpg" width="150" height="146" style="border: 1px solid #C0C0C0" align="right">Looking back, the slowdown has served as a rude wake up call 
					for the sector. “In all unsophisticated markets, there is a 
					lack of willingness to pay for intellectual capital (due 
					diligence and research) and Oman is no different,” says 
					Reddy. As the Dubai model of building gets discredited, 
					there is a growing appreciation of concepts such as 
					segmentation, differentiation and providing value. Says 
					Lora, “Customers are demanding better prices, quality and 
					on-time delivery and developers who do not listen to these 
					demands are going to be at a disadvantage.”<br>
					<br>
					As the ITC and top-end market got hit, a number of 
					developers started developing affordable housing. Zain 
					Properties Development launched Dar al Zain in 2009 with 
					plans to develop 184 town houses and villas over four 
					phases; Elite Properties launched Abraj Zumund in Al Khoudh 
					with 34 two bedroom houses; Towell Alargan is building Al 
					Hail Homes in Al Khoudh over two phases. The project has 126 
					units with a commercial component. <br>
					<br>
					Affordable housing is a loosely defined term with prices 
					ranging from RO30,000 to close to RO100,000. A number of 
					developers misread the market both from a pricing and demand 
					point of view resulting in stalled projects or few buyers. 
					Says Steel, “When the demand for high-end properties dried 
					up, developers started building cheap houses for nationals 
					without doing due diligence about the aspirational value of 
					Omanis which has resulted in delayed projects.” <br>
					<br>
					Omanis have traditionally preferred buying land and building 
					houses to staying in multi-use apartments. But with over one 
					third of Oman’s population still in schools and the number 
