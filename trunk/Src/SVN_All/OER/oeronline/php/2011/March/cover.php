					<font size="2" face="Verdana, Arial, Helvetica, sans-serif">
					The real estate sector in the GCC region got severely 
					impacted by the global subprime crisis forcing developers, 
					banks and property agents to go back to the drawing board. 
					Ironically, this may prove to be the most enduring legacy of 
					the meltdown. A report by Mayank Singh</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					</font></b>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					</font>
					<br>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					Alargan Towell Investment Company’s Al Khairan resort, a 
					top-end property in Bandar Al Khyran, got caught at the 
					wrong end of the global financial crisis. As market 
					sentiment turned adverse and liquidity concerns plagued the 
					real estate market, the project got delayed and the 
					developer had a real problem on its hands. Undaunted by the 
					predicament, Alargan Towell embarked upon something called 
					‘Value Engineering’ – a process which helps to reduce 
					project costs without compromising on the quality of the 
					product. The result was a 12 per cent reduction in the 
					overall cost of the project! <br>
					<br>
					<br>
					Innovation, value for money and revisiting fundamentals have 
					been the guiding principles of the real estate market in the 
					last two years. The market which was dominated by 
					speculators in 2008 has given way to real buyers. Says Ali 
					Hassan Moosa, general manager, Alargan Towell, “One of the 
					indices to measure the real estate market is the number of 
					transactions that have passed through the ministry of 
					housing, and it clearly shows a significant shift.” 
					According to the Ministry of Housing figures, confidence is 
					returning to the real estate sector with transactional 
					activity increasing significantly during the first half of 
					2010, compared to the same period in 2009. The total value 
					of real estate transactions during the first half of 2010 
					was more than RO992mn compared to RO617mn during the same 
					period in 2009 – an increase of 60.8 per cent. The number of 
					real estate transactions involving non-Omani GCC citizens 
					during this period amounted to 2,299 – an increase of 40.7 
					per cent compared to the corresponding period in 2009. 
					Despite this increase, the number of transactions in 2009-10 
					is far less as against in 2007-08. The monetary value of 
					these transactions too has been far lower reflecting the 
					presence of real buyers and sellers in the market.</font><p align="left" style="margin: 3px 10px" dir="ltr">
