					<b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					The ITC space</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					Says Christopher J Steel, managing partner, Savills Oman, 
					“During good times the natural tendency is to build 
					expensive houses and the bubble was based on high-end 
					properties.” <br>
					<br>
					Integrated Tourism Complexes (ITCs) in Oman which were 
					expecting to attract buyers from Europe and the GCC region 
					were in for a rude surprise as global sentiment turned 
					negative. <br>
					<br>
					Blue City, also known as Al Madina A’Zarqa, was launched as 
					Oman’s largest and most ambitious property project in 2006 
					but was bogged down by an ownership dispute, design changes 
					and slow sales. According to a Moody’s report in November 
					2009, Blue City had a total sale of $74.6mn compared to a 
					target of $860mn. The 20-year project was expected to become 
					a city with a population of 200,000 in Barka on the West of 
					Muscat. The first phase was funded by a bond but the 
					developer failed to meet its financial obligations in 2010. 
					Essdar Capital, an investment company from Abu Dhabi has 
					since taken a controlling stake in the project. <br>
					<br>
					Work on the Salam Yiti development got stalled as Sama Dubai 
					got impacted by the debt problems of its parent company 
					Dubai Holding. Dubai Holding is a part of Dubai World which 
					struck an agreement with creditors in October 2010 to 
					restructure a $24.9bn debt. According to IMF estimates, 
					Dubai’s total debt is estimated at $109bn after a 
					credit-fuelled boom unravelled during the global financial 
					crisis. Omran, the ministry of tourism’s investment and 
					development arm, has taken over the project and work on 
					Salam Yiti is expected to resume in the early part of 2011.
					<br>
					<br>
					According to Clutton’s January 2011 Oman Property market 
					report, prices in the ITC re-sales market have declined 
					sharply from the highs of early 2008 but have now stabilised 
					to a degree though the bottom of the market has not been 
					reached. The report foresees a continued but gradual 
					softening of secondary market values over the coming months. 
					Factorising the new market dynamics, The Wave stopped sale 
					of new properties in 2009 and helped existing customers 
					either to get bank credit wherever needed or to sell if 
					necessary. The new properties launched in 2010 were priced 
					20 per cent less compared to the earlier phases. Says 
					Mohamed Ayjaz, general manager, Hamptons International, 
					Oman, “Prices in ITCs fell in the aftermath of the global 
					crisis as people were keen to get their money back. 
					Developers have checked this trend by pricing new stock at a 
					competitive rate compared to the secondary market.”<br>
					<br>
					<img border="0" src="images/cover-2.jpg" width="200" height="172" align="right" style="border: 1px solid #C0C0C0">The seventh property release of 160 apartments and 
					townhouses at The Wave in March 2010 reportedly achieved a 
					95 per cent sales rate within one month of release while the 
					eighth release of properties in early November 2010 included 
					villas for the first time in over two years. The Cluttons 
					report adds that although offered at reduced prices to suit 
					the current market conditions, the recent launch at The Wave 
					was not sold out which indicates that, although confidence 
					and activity are returning, the market is still very much in 
					the recovery phase. The last quarter of 2010 has seen 
					increased activity in the secondary sales market but it is 
					still relatively quiet. The level of enquires is, however, 
					slowly increasing and analysts expect the market to gain 
					momentum during the first quarter of 2011.<br>
					<br>
					Says Jose Lora, CEO and managing director, Juthoor Real 
					Estate, “ITCs have two components – commercial and 
					residential. Though they may start off with a residential 
					component they have to be driven by the 
					hospitality/commercial segment. A number of developers are 
					talking about hotels but little progress has been made on 
					them.” Almost all ITCs have drawn up plans for developing 
					hotels and other leisure options. <br>
					Muscat Hills where the construction of Phase 1 of the 
					residential units is now nearing completion plans to develop 
					a five star hotel, a country club and further residential 
					units.<br>
					<br>
					Saraya Bandar Jissah located in a secluded bay to the East 
					of Muscat is developing 2.2 million square metres of land. 
					This will include residential units, two five star hotels 
					and leisure facilities. The development is a partnership 
					between Saraya and Omran and it is likely to be launched 
					during the early part of 2011. Muriya is preparing to open 
					the Sifawy Hotel during the first quarter of 2011 at their 
					development at Jebel Sifah. The boutique hotel is located in 
					the heart of the marina town and will have 55 guestrooms. 
					The opening of the Sifawy Hotel is expected to increase 
					interest in Jebel Sifah and should have a positive impact on 
					the residential component of the project. The Salam Yiti 
					project will consist of around 2000 residential units, four 
					hotels, a marina and a golf course.</font><p align="justify" style="margin: 3px 10px" dir="ltr">
