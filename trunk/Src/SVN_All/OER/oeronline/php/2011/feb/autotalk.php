				With a combination of stunning looks, a plethora of innovations 
				and environmentally friendly technologies, the new Touareg will 
				become popular among the Middle East customers. Fatma al araimi 
				reports from Dubai<br>
				<br>
				Volkswagen first introduced the Touareg in 2002 and it arrived 
				in the Middle East in 2003. The new, second-generation and most 
				technically innovative model, the 2011 Touareg, which was 
				recently launched, is all new from the ground up but is still 
				recognisable as Volkswagen’s mid-size SUV. Longer, lighter, 
				sleeker, safer and roomier than its predecessor, the 2011 
				Touareg offers the comfort and quality associated with 
				Volkswagen’s products, but with strong performance both on and 
				off-road. The sleek looks reflect the current range of 
				Volkswagen vehicles.<br>
				<br>
				Now weighing 208 kgs less than before, the nimble new Touareg 
				manages to attain 1,642 litres of cargo space despite its 
				improved aerodynamic design, and large variety of new assistance 
				and safety systems. The new Touareg has made dynamics strides – 
				not just with regard to its engines, but also in its looks. The 
				latter are of course attributable to its new styling and an even 
				sportier layout of proportions.<br>
				<br>
				<strong>Elegant appearance<br>
				</strong><br>
				The team led by Volkswagen design head Klaus Bischoff, set out 
				to give the Touareg a significantly more elegant appearance 
				based on the new Volkswagen Design DNA. This applies as much to 
				the new horizontally aligned front end with its distinctive 
				headlights as it does to the rear section typical of the Touareg 
				and its unmistakable night-time rear light design and leaner, 
				athletic body. This has resulted in an SUV that has a 
				sustainable visual effect, timeless styling that makes a 
				qualitative statement, showing dynamic proportions and presence, 
				yet remaining reserved in its elegance and style.<br>
				<br>
				The innovations include bi-xenon headlights with dynamic light 
				assist, and a standard 8-speed automatic transmission – the 
				first of its kind in the world in the SUV segment. The plethora 
				of new innovations also include a pushbutton parking brake, 
				automatic Stop-Start engine shut-off system, electronic oil 
				level display, panoramic sunroof – the largest ever used on an 
				SUV, multi camera operated area view safety system, lane and 
				side assist, adaptive cruise control (ACC) plus front assist, 
				automatic tailgate, and proactive occupant protection.<br>
				<br>
				Fuel economy and emissions values are significantly improved on 
				the conventionally powered Touareg petrol engine version. The 
				petrol engine has a new thermal management system to guarantee 
				that the proper operating temperature is reached quicker, which 
				is important in attaining better fuel economy especially on 
				short driving trips.<br>
				<br>
				The new Touareg also boasts a range of engines that are nearly 
				20 per cent more fuel efficient, and is the first and only SUV 
				of European origin to be available in a hybrid version.<br>
				<br>
				<strong>Safe drive<br>
				</strong><br>
				The new Touareg is also one of the safest passenger vehicles of 
				all times. Bi-xenon headlights with dynamic light assist “sees” 
				oncoming traffic and automatically adjusts the main beam to 
				eliminate unwanted glare. The new area view is a surroundings 
				monitoring system being introduced for the first time at 
				Volkswagen. Via four cameras (in the tailgate, outside mirrors 
				and radiator grille), the area view transfers images of all of 
				the Touareg’s surroundings to the central touch screen in the 
				centre console. Several images can be displayed simultaneously 
				by split screen here.<br>
				<br>
				In off-road driving, front and side cameras are used by area 
				view to transfer images directly in front of and next to the 
				vehicle – which is of inestimable value to the driver on extreme 
				trails. <br>
				<br>
				Adaptive cruise control (ACC) autonomously controls the safe 
				distance to traffic ahead of the vehicle – a cruise control that 
				not only accelerates but also brakes to a stop in an emergency. 
				The space in front of the vehicle is monitored by two radar 
				sensors. Front assist helps to avoid collision accidents. It 
				initiates appropriate protective measures well in advance of a 
