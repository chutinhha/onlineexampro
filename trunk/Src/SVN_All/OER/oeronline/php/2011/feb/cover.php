						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Scaling up confidence</font></b><p class="style15">
				<strong><span class="style14">The General State Budget for 2011 
				is a bookmark in the pages of Oman’s development history as it 
				peeps into the past to propel the country’s prospects. Visvas 
				Paul D Karra finds out what is in store from Budget 2011 and the 
				Eighth Five Year Development Plan<br>
				</span></strong>
						<p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-cover1.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="497" height="315"></font><p align="justify" class="style13">
				The 2010 and 2011 State Budgets are important for the country to 
				overcome the effects of the global financial crisis and to tide 
				over the impact of the crisis. The key factor was of course 
				government spending. The fact that the budgeted spending has 
				increased by more than RO1bn this year is a positive message 
				that the government is committed to development and that gives 
				good a signal to the market,” says Dr Abdullah Salem Al Salmi, 
				executive vice president, Capital Market Authority (CMA).<br>
				<br>
				This statement more or less sums up the underlying message of 
				the State General Budget 2011 and the Eighth Five Year 
				Development Plan which seek to allay any apprehension about the 
				government’s goals for the future, which is to provide impetus 
				to the growth of the country by continued infrastructure 
				spending and investments in human resources development.<br>
				<br>
				“The development of new infrastructure and the improvement of 
				existing facilities will play a significant role in meeting the 
				needs of a fast developing nation, which will come increasingly 
				under the spotlight as the Middle East becomes even more 
				influential,” says Stuart Dunlop, head of ACCA Middle East.<br>
				<br>
				<strong><span class="style16">Consolidating gains</span></strong><p class="style19">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-cover2.jpg" style="border-style: solid; border-color: #808080;" class="style20" width="497" height="184"><br>
				<span class="style21"><em>Dr Abdullah Salem Al Salmi, Executive 
				Vice President, Capital Market Authority</em></span></font><span class="style21"><em>&nbsp;</em></span><p align="justify" class="style13">
				The State General Budget 2011 is unique because it seeks to 
				consolidate the gains of the Seventh Five Year Development Plan 
				and provide a momentum to achieve the objectives laid out for 
				the Eighth Five Year Development Plan, the penultimate plan 
				under the Vision 2020 strategy. Therefore, Budget 2011 estimates 
				are within the fiscal framework of the 8th Plan.<br>
				<br>
				For 2011, the total revenue is estimated to be RO7.2bn while the 
				total expenditure is RO8.13bn. The budgeted deficit is RO850mn.<br>
				<br>
				The government has followed a conservative approach in the 
				preparation of FY2011 budget by assuming the oil price at $58 
				per barrel (bbl) even though the industry estimates oil to hover 
				above 90/bbl during 2011. For the current year, Oman has 
				increased its daily average oil production by 4.2 per cent to 
				896,000 barrels from 860,000 barrels estimated in FY10 budget, 
				which is a progressive sign.<br>
				<br>
				With the crude prices being set at $58 per barrel, the net oil 
				revenue is estimated at RO4.95bn (forming 68.1 per cent of the 
				total revenue), an increase of 22.4 per cent on a YoY basis as 
				compared to 2010 budget. At the same time the gas revenue is 
				expected to surge by 15 per cent YoY to RO920mn. The 
				contribution from this segment to the total revenue forms around 
				12.6 per cent. Overall the total oil &amp; gas revenue continues to 
				remain a major contributor to revenue forming about 80.7 per 
				cent of the total for 2011.<br>
				<br>
				However there has been a drop in the anticipated non-oil revenue 
				for 2011 owing to decline in the estimated other current 
				revenues by 8.2 per cent to RO1.4bn. Overall revenue for 2011 is 
				anticipated to increase by 14.1 per cent to RO7.28bn mainly 
				driven by the growth in the oil sector.<br>
				<br>
				Oman’s total public expenditure for the year 2011 is budgeted at 
				RO8.13bn, an increase of 13.2 per cent YoY with the focus 
				continuing to remain on human resource development and new 
				project spending. The share of current expenditure for the year 
				has increased by 7.4 per cent YoY to RO4.76bn from RO4.43bn with 
				the thrust on job creation, education and healthcare sector. In 
				this, the total expenditure for civil ministries stand at 
				RO2.75bn in 2011 (forming 57 per cent of current expenses), an 
				increase of 10 per cent YoY. This would be used for the ongoing 
				development of basic government services, operational expenses 
				of ministries, growth in education and health services and 
				operational expenses of new projects.<br>
				<br>
				<strong><span class="style16">Past performance</span></strong><p class="style19">
				<font face="Verdana" size="1">
				<em>
				<img border="0" src="images/feb-cover3.jpg" style="border-style: solid; border-color: #808080;" class="style20" width="497" height="184"><br>
				<span class="style21">Dr P Mohammed Ali, Executive Director and 
				Vice Chairman, Galfar</span></em><span class="style21"></span></font><p align="justify" class="style13">
				The 25-year Oman Economic Vision (1996-2020) is the master plan 
				for the development of the non-oil sectors and is popularly 
				called Vision 2020. As such the 2011 estimates are consistent 
				with the main directives of the 8th Plan and within the 
				framework of Vision 2020.<br>
				<br>
				“In spite of the global financial crisis and its repercussions, 
				the national economy realised growth rates that exceeded the 
				planned average annual rate for the 7th Five Year Plan period to 
				reach 13.1 per cent at current prices and 6.3 per cent at 
				constant prices. And the inflation rate, which reached 12.4 per 
				cent in 2008, due to external factors, was reduced to 3.5 per 
				cent at the end of the 7th Plan period,” says HE Ahmed bin 
				Abdulnabi Macki, minister of National Economy and deputy 
				chairman of Financial Affairs and Energy Resources Council, in 
				his 2011 Budget statement where he also unveiled the Eighth Five 
				Year Development Plan.<br>
				<br>
				Non-oil activities recorded strong growth estimated at an annual 
				average rate of 14.6 per cent at current prices and 8.2 per cent 
				at constant prices. This formed a strong base on which the 
				growth of national economy was accomplished, during the 7th 
				Plan.<br>
				<br>
				<strong><span class="style16">Job-creation successful</span></strong><br>
				<br>
				To develop the private sector, a number of improvements were 
				introduced in the regulatory and legal frameworks, which 
				resulted in increased activity of the sector making it the prime 
				employer of the national workforce.<br>
				<br>
				The 7th Plan’s outstanding performance was the impact it had on 
				the labour market and Omanisation. The total additional job 
				opportunities provided to the national workforce during 
				2006-2009 reached 177,000.<br>
				<br>
				Surpluses on current account of the balance of payment and State 
				Budget were realised compared to the planned deficits. The 
				Budget surpluses were directed to strengthen the government’s 
				financial reserve and reduce public debt. Part of the surpluses 
				have been used for the repayment of project funding loans taken 
				from Abu Dhabi Fund for Development (Quriyat-Sur and Al 
				Amerat-Quriyat roads) and Arab Fund for Economic and Social 
				Development (Muscat Expressway and expansion of Salalah Port).<br>
				<br>
				<strong><span class="style16">Stable growth</span></strong><p class="style19">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-cover4.jpg" style="border-style: solid; border-color: #808080;" class="style20" width="497" height="184"><br>
				<span class="style21"><em>Sankar Kailasam, Senior Vice President 
				(Asset Management), GBCM</em></span></font><p align="justify" class="style13">
				“As far as the Eighth Five Year Development plan is concerned, 
				the sustainable levels of fiscal spending and the obvious focus 
				on new job creations is anticipated to bring in long term 
				benefits towards achieving stability in growth,” says Sankar 
				Kailasam, senior vice president (Asset Management), Gulf Baader 
				Capital Markets (GBCM) in his assessment of the budget and the 
				8th Plan. The presence of favourable demographics and the 
				continuance of infrastructure spending will attract foreign 
				investments in awide variety of industries moving ahead. 
				Economic diversification efforts and the creation of workforce 
				are expected to lead to consumption growth, Kailasam opines.<br>
				<br>
				Dr P Mohammed Ali, executive director and vice chairman, Galfar 
				Engineering &amp; Contracting while talking about 2011 Budget’s 
				impact on the construction sector says, “Oman has maintained a 
				consistent growth and it has never ballooned like other 
				countries. When you have a growth, which is consistent and 
				realistically achievable that will continue. That is what Oman 
				is doing. As the economy grows, new developments will take place 
				and this is good.”<br>
				<br>
				In the 8th Plan, the overall allocation towards various 
				development projects like roads, ports, housing, airports, 
				health, education and services sectors has reached about 
				RO12.01bn. This comprises ongoing appropriations carried forward 
				from the 7th Plan amounting to RO6.4bn. The new allocations made 
				during the 8th Plan stand at RO5.57bn.<br>
				<br>
				Dr Ali says the citizens and residents will benefit out of these 
				developments because there is a lot of focus on enhancing 
				infrastructure. “As long as there is infrastructure development 
				in Oman, Galfar will continue to contribute to the growth of 
				Oman. When you have major projects, you need companies that can 
				execute them,” he maintains.<br>
				The 8th Plan aims to achieve a growth rate of not less than 
				three per cent at constant prices and low inflation rates, said 
				HE Macki.<br>
				<br>
				Overall revenues are projected to grow at a stable pace and 
				report a CAGR of 5 per cent over 2010-2015. For the current 
				five-year plan period, the oil revenues are calculated based on 
				the estimated annual average oil production rate of 897,000 bpd 
				and also assuming an average price of $59 per barrel. Oil and 
				Natural Gas revenue is projected to grow by about 5.7 per cent 
				and 4.1 per cent respectively over 2010-2015. In the same period 
				current revenue is projected to grow by 3.2 per cent, GBCM 
				research says.<br>
				<br>
				The overall annual average of total government revenues has been 
				estimated at RO7.49bn for the plan period. Overall expenditure 
				for the 8th Plan period has been estimated taking into 
				consideration social sector spending, project investments and 
				the upcoming oil and gas production schedule.<br>
				<br>
				<strong><span class="style16">Benefits to people<br>
				<br>
				</span></strong>Total current expenditure of the plan period is 
				expected to show a CAGR of 5.5 per cent over 2010-2015. In the 
				same period, civil ministries expenditure (mostly going towards 
				education, health, project developments etc.) is estimated to 
				increase by 7.7 per cent, which would be beneficial for various 
				industry players and local companies.<br>
				<br>
				The Oil and Gas expenditure is estimated to report a CAGR of 4.5 
				per cent over 2010-2015. The annual average public expenditure 
				for the 8th Plan period is estimated at RO8.54bn while the 
				annual average deficit of the budget for the 8th Plan is 
				projected to be RO1.04bn, which is within reasonable levels. The 
				prevailing oil prices above $70 per barrel would minimise the 
				deficit levels during the plan period, says a report by GBCM 
				research.<br>
				<br>
				Commenting on the prudence shown by the government in 
				forecasting this annual average deficit, Dunlop says that this 
				is based on a cautious assumption that oil will sell at an 
				average of $58 a barrel. The average annual price of a barrel of 
				oil in 2010 was in excess of $70 and it is to be hoped that oil 
				revenue will be greater than budgeted for.<br>
				<br>
				<strong><span class="style16">Social spending<br>
				</span></strong><br>
				Dunlop also says that the commitment to develop human resources 
				and to invest in education by constructing new schools, the 
				creation of new scholarships and support for the vocational 
				training sector will ensure that Oman will have a skilled, 
				well-educated workforce. This will be attractive to 
				multinational businesses looking for a location for their 
				regional operations.<br>
				<br>
				The development of national human resources by providing quality 
				education and health are the key focus areas of the strategic 
				five-year plan. The total expenditure on education and health 
				sectors is expected to increase by 52 per cent and 88 per cent 
				respectively as compared to the 7th plan. The plan also focuses 
				on the development of new job opportunities for local people. 
				During the 8th Plan, the government’s endeavour is to provide 
				job opportunities to about 200,000-275,000 people at an annual 
				average of 40,000 to 55,000, which is commendable.<br>
				<br>
				“The emphasis of the 8th Plan on education and healthcare, which 
				is related to the sustainable human resource development, 
				implies that the government is really taking care of its human 
				resources as this is the single most important factor for 
				economic development,” Salmi points out.<br>
				<br>
				<span class="style16"><strong>Development of SMEs</strong><br>
				</span><br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-cover5.jpg" style="border-style: solid; border-color: #808080; float: left;" class="style18" width="100" height="134"></font>During 
				the 8th Plan period, the government has also decided to focus on 
				small and medium enterprises (SME) sector in a big way. As per 
				ACCA estimates, SMEs account for two-thirds of employment (67 
				per cent) and more than half of the output (52 per cent) of the 
				private sector globally. Among developed countries, SMEs are 
				believed to account for 30 per cent of exports and 10 per cent 
				of foreign direct investment and they are seen as the main 
				source of new job creation.<br>
				<br>
				Therefore, says Dunlop, governments have a crucial role to play 
				in supporting enterprise creation, ensuring a level playing 
				field for smaller businesses and building capacity in the SME 
				sector and this represents an exciting opportunity for Oman.<br>
				<br>
				Dunlop observes that the 8th Plan proposals set out a stable 
				economic environment, supported by sound fiscal policy, 
				efficient regulation and a legal system, which are all critical 
				for the development of the SME sector. Furthermore, the growth 
				in the SME sector will help to meet another aim of the Plan, 
				which is to encourage diversification within the business sector 
				and to achieve 10 per cent growth in non-oil activities.<br>
				<br>
				<strong><span class="style16">Shift to non-oil not visible<br>
				</span><br>
				</strong>The penultimate plan for the Vision 2020 is yet to lay 
				enough stress on non-oil revenues and this is one of the crucial 
				points which needs to be addressed, says Salmi. The oil and gas 
				revenues still exceed 80 per cent of the total budget so you 
				have to look deeper into the diversification efforts. “This is 
				important to reduce the contribution of the oil and gas sector 
				in the country’s revenues. And more emphasis has to be put on 
				the industrial and services side. This is my personal view,” he 
				says.<br>
				<br>
				Continuing further, Salmi recalls that we have been seeing a lot 
				of activity in tourism, industrial and projects side in Sohar, 
				Duqm and Salalah. There are also some tourism activities like 
				the recently announced Oman Convention and Exhibition Centre, 
				which will give a boost to the MICE segment. These are good 
				elements. However, we are already in 2011 and still oil and gas 
				dominates the drawing board of the planners.<br>
				<br>
				“As a citizen of this country, I would like to see the 
				diversification process speeded up because with Duqm developing 
				along with Sohar and Salalah, it could be a good idea to 
				encourage industries to depend upon the oil and gas as their 
				feedstock and add value instead of exporting most of the 
				hydrocarbons,” explains Salmi.<br>
				<br>
				He feels that it is a step-by-step progress towards achieving 
				Vision 2020 but the country will reach there as we are on the 
				right track.<br>
				<br>
				<span class="style16"><strong>Develop trading<br>
				</strong></span><br>
				The notable move Oman could make is to take a step back in 
				history to find out the recipe for its diversification strategy. 
				Since Oman has a long history of being a trading nation, the 
				country should remain a trading nation, says Salmi, 
				emphatically. <br>
				<br>
				He believes that the Sultanate should grab the trading 
				opportunities because of the country’s strategic location at the 
				cross roads of the East-West and North-South shipping routes. 
				Alluding to OER’s January 2011 Cover story ‘Docking Growth’, 
				Salmi says that we should really bank on becoming a trading hub 
				as we now have the facilities: Sohar (very close to the GCC), 
				Duqm (open to Indian Ocean) and then Salalah (not far from 
				Africa).<br>
				<br>
				Trading (exports and re-exports) should become the leading 
				sector when we talk of the diversification process. Since, 
				Oman’s population is small and cannot sustain the manufacturing 
				industry on its own, we can focus on industries that are export 
				oriented and even re-exporting. This has been the main activity 
				since ages ago and we should not divert from that, Salmi says.<br>
				<br>
				The good news though is that government’s initiatives as per the 
				previous plan periods to diversify have yielded fruits. The 
				growth of non-oil activities has almost doubled during the 7th 
				Plan period to 14.6 per cent as against expected numbers of 7.6 
				per cent. The key growth area in the non-oil activities being 
				agriculture, industrial and the services sector.<br>
				<br>
				Agriculture and fisheries grew at a CAGR of 7.2 per cent as 
				compared to an expected growth of 2.7 per cent. The industrial 
				segment has shown a growth of 18.4 per cent thanks to mining and 
				quarrying (up by 26.6 per cent); manufacturing of refined oil 
				products (23.2 per cent); manufacturing of chemicals and 
				chemical products (20.2 per cent); and construction (22.7 per 
				cent). The Sultanate has built a strong economic foundation in 
				pursuance of Vision 2020, this has gone hand in hand with social 
				sector spending. Budget 2011 and the 8th Five Year Plan are in a 
				vantage position to build on this and take the country to new 
				heights.</td>
