						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Know your 
						banker</font></b><p align="justify" class="style3">
				<strong><span class="style5">A high powered delegation from the 
				145-year-old Espirito Santo group visited the Sultanate to 
				explore business opportunities. Mayank Singh reports</span></strong><span class="style5"><br>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-banking1.jpg" width="250" height="149" style="border: 1px solid #808080; float: right;" class="style4"></font><font size="1">To 
				swim in unchartered terrain is what Jose Manuel Espirito Santo 
				Silva, president du conseil d’administration, Banque Privee 
				Espirito Santo seems to thrive on. An inheritor of Espirito 
				Santo – one of the biggest business empires in Portugal, Silva 
				saw his bank being nationalised – an euphemism for a blatant 
				confiscation of the family business by the state during the 
				Carnation Revolution of Portugal in 1974. Undeterred he moved to 
				Europe with the intention of providing financial services to the 
				Portuguese diaspora.<br>
				<br>
				In 1977 Silva established the Compagnie Financiere Espirito 
				Santo in Lausanne, Switzerland. The institution grew into a 
				full-fledged bank over the years with the name – Banque Privee 
				Espirito Santo (Espirito Santo). Since then the bank has spread 
				to Brazil, Spain, France, Eastern Europe, Africa and the Middle 
				East. The group has assets worth 90bn euros and its profits in 
				2009 stood at 160mn euros. Having started with a handful of 
				people the group today has 7,000 people working for it.<br>
				<br>
				Sensing potential in Oman, Silva visited Oman recently with a 
				high powered delegation from Espirito Santo. “We are happy with 
				the response that we have got in the Sultanate. It’s been an 
				opportunity to see the potential of the country and to meet 
				people here. We look forward to assisting family businesses and 
				are exploring future opportunities,” says Silva.<br>
				<br>
				Apart from private banking Espirito Santo provides services like 
				commercial banking, project finance and corporate finance. The 
				bank has a strong project and corporate finance team in London 
				and Silva sees a strong potential in these areas. Espirito Santo 
				was the fifth largest lead arranger of project finance globally 
				in 2009. “We have a good knowledge and expertise in developing 
				businesses in a wide range of countries and are keen to bring 
				this to Oman. We are not trying to compete with banks here to 
				provide commercial banking as the banks in the region are well 
				established,” adds Silva.<br>
				<br>
				Says John Lochrie, regional director, Espirito Santo, “Our 
				purpose in bringing a senior delegation was to understand the 
				environment and to build relationships in Oman. Whatever the 
				family does is based on certain principles and partnerships.” 
				The bank has got requests from Oman based businesses to provide 
				them with assistance in Africa and Latin America either to 
				expand their prevailing businesses or to set up shop. The bank 
				plans to follow up on these by organising a trip by its project 
				finance team to the Sultanate. The group has had successful 
				banking operations in Dubai for the last four years.<br>
				<br>
				<strong>Africa – the new frontier<br>
				</strong><br>
				Espirito Santo has been pursuing a strategy of geographical 
				diversification. It has a strong presence in Brazil, Angola, 
				Libya and Morocco and is in the process of buying a stake in a 
				bank in Mozambique. Talks are also on to buy a bank in South 
				Africa. Though Portugal and Spain remain the mainstay of its 
				revenues, the bank’s foreign operations contribute to over 35 
				per cent of its income. Says Silva, “We have had a presence in 
				Angola since 2001 and had consistently good results there. 
				Espirito Santo is the third most profitable bank in sub-Saharan 
				Africa. ”<br>
				<br>
				The bank’s success in Africa is the result of a number of 
				factors. One, it has been in Africa for the last 60 years. This 
				has given it high brand recall and recognition. Secondly, the 
				group has focussed on growth areas like agro industrial food 
				projects, sugar and coffee apart from banking and insurance. 
				Finally, the bank has exhibited its steadfast commitment to the 
				African market. For example, during the civil war in Angola from 
				1998-2002, Espirito Santo not just ventured into the market but 
				remained there till the end of the war. The investment paid off 
				as business took off once the war ended. “We are very happy with 
				our presence and as a family policy we want to be present in 
				Africa in future,” says Silva.<br>
				<br>
				<strong>Values come first<br>
				<br>
				</strong>Though conventional wisdom suggests that family 
				businesses do not survive beyond three generations, the Espirito 
				Santo family is now in its fourth generation. Ask Silva about 
				the reasons for this and he says, “We have a great asset – which 
				is the name of our family. We have good relations with other 
				financial institutions worldwide and these have existed for 
				generations. With our name and good team we have overcome 
				difficulties.” The Portuguese diaspora has been a bedrock of 
				support for the group enabling it to grow across the world. The 
				bank has a number of clients who have been serviced for 
				generations. Similarly a number of strong international 
				companies have had a long standing partnership with the bank. 
				For example, Credit Agrico from Brazil has banked with Espirito 
				Santo for decades.<br>
				<br>
				The family stresses on respect within various generations. Says 
				Domingos Espirito Santo Pereira Countinho, advisor to the 
				executive committee and a fourth generation member of the 
				family, “In our education in Europe we learn a lot about 
				respecting the knowledge and experience of superiors within the 
				family. They in turn give us an opportunity to share our ideas 
				with them. Secondly our communication is very open, we can share 
				our ideas with the leaders of the family.” Moreover, various 
				generations are given a chance to take responsibilities within 
				the business, which takes care of their aspirations. As the 
				business has grown there has been a need to look beyond the 
				family and Espirito Santo has professionalised it’s business. 
				“The group is controlled by a family but it is not run by the 
				family. We firmly believe in corporate governance within the 
				business,” says Countinho.<br>
				<br>
				<strong>Transparency issues<br>
				</strong><br>
				Speaking about corporate governance an oft-repeated charge 
				against Switzerland based banks is that they thrive on secrecy 
				and non-disclosure. As regulators worldwide insist on stricter 
				governance codes as a part of Basel III norms, will banks like 
				Espirito Santo be forced to change its business model? Says 
				Silva, “We have to respect the laws. The Swiss authorities are 
				studying the laws and a final decision on disclosure norms is 
				yet to be taken. We have a lot of respect for bank secrecy and 
				believe that it is very important for our business but we will 
				change if new laws come into place. It would be wrong to 
				attribute our success only to secrecy. The success of Swiss 
				banking is based upon performance, access to good people and a 
				number of other factors.”<br>
				<br>
				Banking is a service where one needs to know one’s clients. Says 
				Silva, “if you see my card, its says ‘Know your banker’ on the 
				reverse. This important as we want to have direct contact with 
				our customers.” With a never-say-die spirit being the middle 
				name of Espirito Santo, the group can look forward to the next 
				century confidently.</font></td>
