				MSE. Thus a large portion of my working time is spent being with 
				and talking to my people. I believe I am intrinsically an open 
				and a transparent person and therefore naturally inclined 
				towards what’s generally referred to as ‘participatory 
				management style’. I also believe that once a decision is taken, 
				then everyone has to be a disciplined soldier and implement it 
				in spirit.<br>
				<br>
				<strong>What is your strength and how could you utilise it for 
				the advantage of Mustafa Sultan?<br>
				<br>
				</strong>My core leadership strengths lie in being able to see 
				the big picture, forecast trends, read the signals and to 
				identify futuristic market opportunities and business models. 
				Synthesising all these into a cohesive idea and then 
				articulating it in a manner where senior managers can develop 
				their micro-plans for their respective business units is where I 
				see myself contributing the most.<br>
				<br>
				<strong>How do find a balance between your work and family 
				commitments?<br>
				<br>
				</strong>I don’t mind long hours because I really enjoy the 
