						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Turning a leaf</span></strong><p class="style15">
				<strong><span class="style14">The Teejan Group of Companies, in 
				its four decades of service to the nation, has carved out an 
				indisputable reputation of prompt delivery and quality work 
				through its highly diversified operations. The company is 
				presently undergoing a period of restructuring to strengthen and 
				synergise its activities, says I Musheer Ahamed, CEO. An OER 
				report</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-realty1.jpg" width="497" height="155" style="border: 1px solid #808080; "></font><p class="style15">
				What is in common between Lulu and Safeer Hypermarkets, a local 
				Omani school and a regional health centre? Or for that matter, 
				what has a can of Rani Fruit Juice got to do with the above 
				mentioned four? No prizes for guessing, but the answer is Teejan 
				Group. The can of Rani is distributed by Teejan while the Lulu 
				and Safeer and most of the Omani schools and regional hospitals 
				have been built by the construction division of Teejan group of 
				companies. There is also a strong likelihood that the fire 
				fighting equipment hanging in many of the buildings are also 
				distributed or installed by Teejan’s fire services arm.<br>
				<br>
				Indeed, it would not be an exaggeration to say that Teejan has 
				permeated all levels of your life with a vastly diversified 
				range of activities and businesses to emerge as one of the most 
				prominent and leading conglomerates in Oman. Established in 
				1974, Teejan Group’s wide spectrum of activities and services 
				include construction; fire &amp; security engineering; laboratory 
				engineering; environmental engineering; electromechanical; food 
				&amp; beverage; cleaning &amp; equipments; furnishing and information 
				technology.<br>
				<br>
				<strong>Growing reputation<br>
				</strong><br>
				From its humble beginning as a building construction company, 
				Teejan Group established its credentials to take up civil works 
				for large industrial infrastructure projects. And thanks to the 
				rapid economic development, which took place in the last two 
				decades, Teejan’s reputation as a company to reckon with grew 
				with every project it successfully executed.<br>
				<br>
				The latest and one of the biggest projects bagged by Teejan is 
				the civil construction works for the $1bn Salalah Independent 
				Water and Power Plant (Salalah IWPP) in Dhofar Governorate. 
				Teejan is executing this project for SEPCOIII Construction LLC.<br>
				<br>
				Teejan Fire Engineering Division is currently executing the EPC 
				contract for Salalah Power Project (SEPCOIII) and Oman 
				Industrial Complex Phase- I for Vale in Sohar. Teejan Fire 
				Engineering division has a reputation for providing EPC services 
				to most of the power projects in Oman.<br>
				<br>
				Teejan Trading Division will be another area of focus to 
				increase the portfolio of products and services, apart from 
				laboratory furniture.<br>
				<br>
				With the scope and quantum of work increasing with each passing 
				year, the management of Teejan decided to streamline business 
				activities under a restructuring programme. As a first step, 
				they decided to appoint I Musheer Ahamed, as the CEO of the 
				company in October 2010.<br>
				<br>
				It was a strategic move as Ahamed, having worked earlier with 
				the company for four years from 2004, is no stranger to Teejan. 
				The comfort levels were high and his second coming to Teejan was 
				well received not only by the staff but more importantly, the 
				external clients, vendors and other contacts who gave him the 
				thumbs-up sign. This was indeed a good indicator about the 
				management’s business acumen and foresight.<br>
				<br>
				Prior to his current role, Ahamed was the vice president of 
				Lanco Infratech at the 2x600MW Udupi Thermal Power Project in 
				India where he was responsible for project management of 
				construction of the main plant and BOP contracts. He was 
				associated with Reliance Group Companies, L&amp;T – Sargent &amp; Lundy, 
				Voest Alpine India and Jaiprakash Industries. He holds a 
				Bachelor of Civil Engineering and has extensive experience 
				across the construction industry. <br>
				<br>
				Ahamed brings with him a repertoire of experience having worked 
				with very large corporate companies, client/development 
				companies, MNCs, engineering companies, medium to large 
				contracting companies in India and Middle East, adding immense 
				value to Teejan.<br>
				<br>
				<strong>Organisational structure<br>
				</strong><br>
				Talking about his vision and how he aims to take Teejan forward, 
				Ahamed says that his main job is to restructure the existing 
				set-up and build an organisation of experienced and diversified 
				professionals. “We definitely want to have a corporate culture 
				unlike many individual-centric organisations. We want to have a 
				flat structure and give freedom to our next level of managers 
				and SBU heads to handle their portfolios independently,” he says
				<br>
				<br>
				Ahamed is confident that the excellent relationship he shares 
				with the management will help him to achieve this objective. 
				“The management has given me full freedom to run this 
				organisation while they continue to support me with the new 
				business developments and bringing in the leads,” he says.<br>
				<br>
				“The company restructuring process will go on until March as we 
				continue to refocus ourselves internally. This is my primary 
				objective. We are recruiting staff from different backgrounds as 
				we do not want to recruit from one area of specialisation or 
				country so that our capabilities will not be limited,” avers 
				Ahamed while talking about the new organisational structure. <br>
				<br>
				“We will recruit from India and other countries. Presently, we 
				have two Iraqi managers and we are open to recruiting people of 
				any nationality as long as they are competent in their fields 
				because we are working on big tenders in GCC and the African 
				markets,” Ahamed says.<br>
				<br>
				Ahamed’s wide-spread experience in construction engineering and 
				project management for various industrial sectors such as power, 
				refinery, telecom, real estate, etc will stand him in good stead 
				to lead the company and position it at the forefront of the 
				industry by developing strategic plans to advance the company’s 
				mission and objectives.<br>
				<br>
				<strong>Expanding borders<br>
				</strong><br>
				As part of its expansion plans, Teejan is certainly looking 
				beyond Oman. Ahamed discloses that the company has pitched in 
				for some construction and infrastructure projects in other GCC 
				countries and Africa and the work will start in the last quarter 
				of the year. Entering into joint ventures with Indian and 
				European companies is another plan on the anvil for 
				diversification into specialised sectors.<br>
				<br>
				“I would like to see the company grow by over five times in the 
				next five years. We can achieve this by tapping potential 
				markets and playing out our strengths. There is a huge scope to 
				expand our MEP division, which presently caters to in-house 
				activities. But I am in the process of strengthening and making 
				a separate division to take care of external projects. 
				Establishing the identity of Teejan as an MEP contractor is 
				important as it has numerous advantages,” asserts Ahamed. <br>
				<br>
				Teejan also wants to venture into the market as a developer and 
				investment partner in real estate as well as industrial projects 
				as there is a lot of scope for downstream projects. They are 
				already working on three warehousing projects, Ahamed adds.<br>
				<br>
				The company has also set its sights on strengthening its 
				logistics business with the help of its Indian partner to 
				operate in Oman and the African region. This will complement the 
				group’s infrastructure operations in railways, roads, ports and 
				airports.<br>
				<em><br>
				With such high aims, Teejan is certainly cut out to go places.</em></td>
