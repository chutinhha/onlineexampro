						<span class="style27">The beckoning hills</span></strong><p class="style15">
				<strong><span class="style14">OER CEO Golf 2011, one of Oman’s 
				biggest corporate networking events, will tee off at Muscat 
				Hills Golf &amp; Country Club on February 3, 2011</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-golfupdate1.jpg" width="497" height="204" style="border: 1px solid #808080"></font><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>There 
				are a few occasions in the crazy hustle of corporate life when 
				CEOs get to leave the brainstorming of the boardroom and settle 
				in for some quiet relaxation. But for Oman’s head honchos, OER 
				CEO Golf 2011 has other plans. The seventh episode of the 
				country’s elitist leisure golfing event will be a day of 
				recreational reckoning that will pit CEOs in a battle of guts, 
				grit and glory on the greens. Muscat Hills Golf and Country Club 
				plays host, while Nawras Business Solutions is presenting the 
				corporate networking event of the year.<br>
				<br>
				Ray Stopforth, Director of Golf (and a PGA Professional) – 
				Muscat Hills Golf and Country Club, says, “We are proud that the 
				OER CEO Golf has chosen Muscat Hills Golf &amp; Country Club to host 
				this prestigious event. Events such as this give Muscat Hills 
				greater exposure to golfers and non golfers throughout the 
				Middle East.”<br>
				<br>
				According to Markus Golder, Business Segment Director – Nawras, 
				“We are enthusiastic about supporting activities that appeal to 
				our customers and events that help to spread the excitement and 
				innovation of the Nawras brand to an even wider audience.”<br>
				<br>
				Adding to the power-packed performances on the greens will be 
				the debut of the INFINITI high-performance range of cars from 
				the renowned Suhail Bahwan Automobile stable. INFINITI is the 
				associate presenter. “This marks INFINITI’s entry into OER CEO 
				Golf and we are happy to showcase our line of performance driven 
				vehicles,” says Bibhudendu Pattanaik, head of marketing – 
				INFINITI, Suhail Bahwan Automobiles LLC. The official timekeeper 
				of the event is ROLEX from Khimji’s Watches. “We are very happy 
				to be part of this prestigious event. It is the best event in 
				Oman. We wish all players the best of luck and hope that they 
				play hard to win a Rolex,” says Madhursinh Jesrani, general 
				manager, Khimji Watches.<br>
				<br>
				Sandeep Sehgal, CE, United Media Services says, “With each 
				edition of CEO Golf we strive to incorporate new and exciting 
				elements in the game and this time is no different.”<br>
				<br>
				And, indeed this edition of CEO Golf has lots to keep you on 
				your toes. The action will tee off with a round of rapid-fire 
				Power Play Golf, followed some light-hearted SNAG. Participants 
				will also get to see a pro in action – world renowned Trick Shot 
				specialist, Jeremy Dale. It’s no less than a red-carpet cultural 
				extravaganza in the evening featuring electrifying performances 
				by the UK-based, all-girl string quartet, Asteria. Rounding off 
				the event is a gourmet banquet and glittering awards ceremony 
				with an array of exciting prizes.<br>
				<br>
				Publicity partners are Times of Oman and Al Shabiba; Media 
				sponsors include Hi, and Al Youm Al Sab’e; prize partner is 
				Samsung. Category partners are Signature (Luxury) and AXA 
				(Insurance), Reach (Activation), Oman Printers &amp; Stationers 
				(Printing), OUA (Beverage), Infoline (Call Centre) and Aggreko 
				(Energy). Prize partners include Samsung, Micromax Mobiles, 
				Damas, Millennium Resort, Shangri-La’s Barr Al Jissah Resort &amp; 
				Spa, Travel City and Graphic International Centre.</td>
