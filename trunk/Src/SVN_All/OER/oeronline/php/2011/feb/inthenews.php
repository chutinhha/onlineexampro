						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Oman – A MICE destination in the 
						making</span></strong><p class="style15">
				<strong><span class="style14">State-of-the-art conference and 
				exhibition facilities to be complemented by four hotels at the 
				Oman Convention and Exhibition Centre</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-inthenews1.jpg" width="497" height="331" style="border: 1px solid #808080"></font><p align="justify" class="style13">
				Omran has formally broken ground for its new major project ‘Oman 
				convention &amp; exhibition centre’ which is expected to be one of 
				the region’s leading facilities for conferences and exhibitions.<br>
				<br>
				HE Ahmed bin Abdulnabi Macki, minister of national economy and 
				deputy chairman of financial affairs and energy resources 
				council, laid the foundation stone of the centre at a ceremony 
				attended by H.E. Dr. Rajiha bin Abdul Ameer bin Ali, minister of 
				tourism and chairperson of Omran and many distinguished guests.<br>
				<br>
				The facilities of the centre include an auditorium with a 
				seating capacity of 3,000, exhibition halls at an area of 
				25,000sqm with seating for 10,000 people in the first phase, 
				banquet halls, meeting space, office space at an area of 85,000 
				sqm in a grade-A business park, four hotels with a total of 
				1,000 rooms and a large shopping mall. Located just four 
				kilometres from Muscat Airport, the Oman Convention &amp; Exhibition 
				Centre will act as a gateway to Muscat. Its landmark design will 
				draw on Oman’s strong cultural heritage and architectural 
				traditions, taking full advantage of the natural beauty of its 
				surroundings. <br>
				<br>
				Oman’s first international-standard convention centre which 
				represents a major investment by Omran, this will allow Muscat 
				to become a regional destination serving the MICE (meetings, 
				incentives, conferences and exhibitions) sector, providing an 
				additional boost to Oman’s fast-growing tourism industry.<br>
				<br>
				Says HE Macki, “The facility will allow Oman to become a major 
				new destination for conferences, exhibitions and other major 
				business events. These will have a major impact on our economy 
				in the future, creating many hundreds of jobs for Omanis and 
				attracting hundreds of thousands of business travellers each 
				year.”<br>
				<br>
				According to HE Rajiha, the centre represents an important 
				element of Oman’s tourism strategy. “Over recent years, Oman has 
				taken major strides forward in attracting tourists to our many 
				leisure resorts and hotels. Our strategy has always been to 
				target the business sector too and this investment will provide 
				us with an outstanding venue for conferences and exhibitions,” 
				says Rajiha. <br>
				<br>
				Wael bin Ahmed Al Lawati, CEO of Omran, says Omran felt 
				privileged to be chosen to develop this strategic project. “We 
				have completed many important stages of the design and planning 
				work for the centre and are delighted to have started work on 
				site. We have been given an ambitious timeline to complete the 
				first phase of the project but as we showed with the site for 
				the Asian Beach Games, we can rise to the challenge,” adds Al 
				Lawati. <br>
				<br>
				Omran is mandated by the government of Oman to deliver major 
				projects and manage tourism assets and investments in the 
				country.</td>
