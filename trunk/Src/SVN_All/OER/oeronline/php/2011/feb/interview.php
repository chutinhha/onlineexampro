						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Quality matters</span></strong><p class="style15">
				<strong><span class="style14">Global Omani Development &amp; 
				Investment Company is looking to expand its projects geography 
				to cover all parts of the Sultanate, says CEO, Mohammed Abu 
				Baker Al Ghassani</span></strong><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-interview1.jpg" style="border: 1px solid #808080; float: right;" class="style28" width="150" height="199"></font><strong>GLOREI 
				set up operations in 2008 with a view to contribute to the 
				diversification of Oman’s economy. How has the company’s 
				experience been in the last two and a half years?<br>
				</strong><br>
				Global Omani Development &amp; Investment Company (GLOREI) is a 
				leading real estate developer in Oman. It is a closed 
				shareholding company established in 2008, with a paid capital of 
				RO43.5mn. The shareholders are a group of leading financial and 
				investment companies in the Gulf with a clear strategic vision 
				consistent with the founders’ commitment to participate in the 
				economic development of GCC countries in general, and Oman in 
				particular, taking the real-estate development sector as the 
				base of our investments. This is a vital and important sector 
				witnessing interesting activities. In terms of paid-up capital, 
				our company is the largest real estate development company in 
				Oman.<br>
				<br>
				<strong>Can you give us an overview of the various projects that 
				the company is executing presently?<br>
				</strong><br>
				GLOREI started the development of a five-star hotel Shaza Muscat 
				Hotel in Al Khuwair. It is about 16 kms away both from the 
				Muscat International Airport and the central business district 
				(CBD) in greater Muttrah. The project is built-up on a stretch 
				of land of about 12,566sqm, and is being developed by GLOREI 
				along with its strategic partners. This property will be 
				operated by a well-known international hotel brand, maintaining 
				high standard of hospitality.<br>
				<br>
				In addition, we have also set up the sophisticated Business 
				Centre (Maktabi Al Khuwair) located in the prestigious business 
				district and diplomatic area. The business centre provides 
				premium serviced office space with managed facilities on a 
				short, medium, or long-term basis. Another project which is 
				underconstruction is Maktabi Al Watiyah, an environment friendly 
				commercial project located in Wattayah. The project itself 
				offers most modern and environment-friendly showrooms, 
				beautifully designed offices and a business centre in a 
				strategic central location of Muscat city.<br>
				<br>
				The emergence of positive signs for the recovery of the global 
				economy have brought the founders together in establishing Al 
				Muttawar Omani Company SAOC as a closed joint stock company. The 
				idea of establishing this company is in line with the strategic 
				vision of GLOREI. This will help to take advantage of investment 
				opportunities available in the market through the development 
				and implementation of residential, commercial and tourism 
				projects. The development, with a total built up area of around 
				70,000sqmts will cost approximately RO35mn and is expected to be 
				completed within 36 months.<br>
				<br>
				<strong>GLOREI has set up a subsidiary logistics solutions 
				company. Are there synergies between the real estate and 
				logistics business or is it an independent profit centre?<br>
				</strong><br>
				GLOREI has contributed to the establishment of Omani integrated 
				logistics company, an Omani closed joint stock company with a 
				group of investors from within and outside the Sultanate. The 
				company commenced its operations in 2009 providing internal 
				logistics solutions. During the last year, it worked towards 
				strengthening its fleet of trailers and cooling trucks in order 
				to meet the market demand. The company also acquired a plot of 
				land in a prime location near Muscat, with an objective to 
				develop a multi-segment warehouse, which would cater to large 
				retailers and shipping companies, among others.<br>
				<strong><br>
				GLOREI entered the real estate business at a time when the world 
				went through a global financial crisis, did this impact your 
				business and on hindsight do you feel that you entered the 
				business at a wrong time?</strong><br>
				<br>
				The impact of the global financial crisis made us rethink and 
				rationalise ambitious plans which were made when the company was 
				established. The economic instability prevalent till recently 
				led us to apply business plans that were needed to combat the 
				impact of the economic crisis. While the crisis affected Oman in 
				one way or another especially the real estate sector, we decided 
				to focus more on studying market needs.<br>
				<br>
				This also meant utilising opportunities that arose from the 
				financial crisis, considering that the company is young and has 
				strong liquidity. We are now working on several projects as 
				mentioned above, and evaluating other opportunities. We consider 
				it as a good opportunity as developers to undertake certain 
				projects, considering the favourable prices of development 
				inputs, like the prices of building materials, the land itself, 
				and the improvement in demand.<br>
				<br>
				<strong>GLOREI has been a strong proponent of the environment 
				cause and was awarded at the Cityscape Awards for its commitment 
				to the environment. In what ways are your projects contributing 
				to environment preservation?</strong><br>
				<br>
				Cityscape award is the highest honour granted in recognition of 
				highly distinguished eco-friendly real estate projects in the 
				region and emerging markets, and thus underscores GLOREI’s 
				commitment to cleaner environment and sustainability in terms of 
				design and construction. The project was conceived and designed 
				out of an ambitious element of GLOREI’s efforts towards 
				sustainable development. It also seeks to have an effective 
				impact on the visitors to the site to achieve the desired growth 
				of the tourism sector and its components, an important and 
				expanding tributary of the national economy.<br>
				<br>
				The idea of the project originated while exploring various 
				options to developing the company’s land adjacent to Khawr Awqad 
				in Salalah. The thought of establishing tourist facilities to 
				serve this area was based primarily on maintaining the essential 
				elements of the existing environment at the site and cultural 
				heritage of Oman.<br>
				<strong><br>
				The company has been associating itself with socially relevant 
				platforms like Brand Oman and Origin Oman. What is the idea 
				behind such associations?</strong><br>
				<br>
				The Brand Oman initiative is of great importance to the 
				Sultanate as we assert ourselves in today’s increasingly 
				competitive global marketplace. We need to let the world know 
				about the excellence of Oman, its products and services. Led by 
				a clearly defined international image we can attract more 
				business, more investment and more tourism. The entire country 
				can benefit. I firmly believe that a well-engineered nation 
				branding programme will create lasting value for Oman.<br>
				<br>
				With regard to Origin Oman, GLOREI was particularly interested 
				in the Go Green Guide produced by Origin Oman. Environmental 
				stewardship is very important to us; responsibility to the 
				environment is at the heart of GLOREI’s company values. Origin 
				Oman encourages us all to use local suppliers and by doing so we 
				can really help the environment as the goods have fewer 
				kilometres to travel and therefore have a reduced carbon 
				footprint – there’s less transport related pollution.<br>
				<br>
				<strong>Looking ahead, how do you plan to take the company 
				forward in the next few years?<br>
				</strong><br>
				We plan to be consistent in our growth, provide comprehensive 
				real estate solutions for our targeted segments, and expand our 
				projects geography to cover all parts of Oman.</td>
