						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Mazoon Reduces Distribution System 
						Losses</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<strong><span class="style14">Mazoon has set distribution system 
				losses reduction as one of its strategic objectives</span></strong></font><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-power1.jpg" style="border: 1px solid #808080; float: right;"></font>As 
				Mazoon Electricity Company has succeeded in reducing the 
				distribution system losses every year since its inception in 
				2005. The company was able to achieve more than the reduction 
				target set by the Authority for Electricity Regulation (AER) 
				consecutively for 2009 and 2010. While the annual target for 
				reduction set by AER was 1.90 per cent, the company was able to 
				achieve 3.60 percent in 2009 and 2.40 per cent in 2010 (as per 
				initial calculations) compared to the respective previous years, 
				thereby reducing approximately 6.0 per cent over the two years 
				compared to the targeted 3.80 per cent.<br>
				<br>
				A number of initiatives and measures taken by the company have 
				contributed to achieve the reduction. The company has set 
				distribution system losses reduction as one of the strategic 
				objectives and allocated responsibilities to departments and 
				employees.<br>
				<br>
				<strong>Management Team<br>
				</strong><br>
				Besides, it has formed ‘central distribution system losses 
				management team’ and ‘regional distribution system losses 
				management teams’ with representation from various departments 
				having knowledge, experience and skills in technical and 
				commercial matters. Annual targets were set for the teams, 
				considering the targeted distribution system loss at the end of 
				2016 and annual target set by AER. The teams meet monthly to 
				diagnose the causes of distribution system losses and to follow 
				up on the various actions assigned to the departments to reduce 
				the losses.<br>
				<br>
				<strong>Earmarking Funds<br>
				</strong><br>
				Other measures taken to reduce distribution system losses 
				include earmarking adequate funds in capital expenditure budget 
				for investment in capacitors, replacement of the faulty metres 
				etc. The company also gives special emphasis on locating the 
				substations to be close to load centres and also ensures 
				completion of projects and reinforcement of the network before 
				the summer. Better metre reading and billing management also 
				helped in this achievement.<br>
				<br>
				Moreover, the company has also encouraged research and studies 
				on distribution system losses. Four such studies have been 
				undertaken.</td>
