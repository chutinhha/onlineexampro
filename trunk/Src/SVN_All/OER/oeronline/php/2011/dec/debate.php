				Stage set for intensive Oman Debate 2011<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana"><b><font style="font-size: 9pt">Oman Debate 
				2011, the most awaited annual business forum in corporate Oman’s 
				calendar, will be held on December 14, 2011 at Oman Auditorium, 
				Al Bustan Palace Ritz Carlton Hotel.<br>
				</font></b></font><font face="Verdana" size="1"><br>
				<img border="0" src="images/debate_dec1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="192" align="right">Oman 
				Debate 2011, one of the most eagerly expected events in the 
				Sultanate, continues to generate tremendous excitement in the 
				corporate world of the country. Anchored by renowned television 
				host Tim Sebastian, the founder and chairman of ‘The Doha 
				Debates’ and former BBC Hard Talk presenter, the debate will 
				feature a distinguished panel of speakers to discuss core 
				issues. <br>
				<br>
				The annual national debate is organised by Oman Economic Review 
				(OER), the leading business magazine in the Sultanate, in 
				association with the Capital Market Authority (CMA). The debate 
				encompasses key themes that form the business environment of 
				today and tomorrow in Oman. Thanks to the grassroots issues 
				tackled in a healthy atmosphere where representatives from both 
				the public and private sectors are present, a lot of excitement 
				has been generated around the third edition of the event.<br>
				<br>
				The strategic partners for Oman Debate 2011 are Bank Sohar, 
				Nawras, Genetco Canon, Audi and Ominvest while the support 
				partners are Al Habib, Taageer Finance, Sharq Sohar Steel and 
				GLOREI. The media partners will be Times of Oman and Al Shabiba 
				newspapers while the Official call centre is Spanco.<br>
				<br>
				Oman Debate 2011 is raising various socio-economic issues like 
				job creation, raising salaries, sops and benefits and 
				Omanisation for deliberation during the Debate. It will also 
				discuss whether the Sultanate is at the crossroads of a 
				watershed moment in Oman’s socio-economic history where 
				everything that was held as sacred needs to be questioned and 
				redefined.<br>
				<br>
				This year’s discussions will be heavily focused on how 
				government and private companies plan to deal with subjects such 
				as job creation for Omani nationals and the impact of rising 
				salaries on companies and the economy at large.<br>
