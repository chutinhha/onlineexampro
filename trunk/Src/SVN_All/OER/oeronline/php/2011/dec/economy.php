						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="4" color="#C00000">Sectoral 
				Symbiosis</font></b><p align="justify" style="margin: 3px 10px">
				&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="2"><b>A look at recent economic 
				investments in the Sultanate reveals significant potential for 
				various types of economic symbiosis</b></font><font size="1" face="Verdana"><br>
				<i>BY Oliver Cornock</i></font><font face="Verdana" size="4" color="#C00000"><b><br>
&nbsp;</b></font><p align="justify" style="margin: 3px 10px">
				<font size="1" face="Verdana">Long before Pixar’s animated film 
				Finding Nemo catapulted clownfish into the international 
				spotlight, these little orange sea creatures were known for 
				something else: their curious relationship with sea anemones. 
				Clownfish live in the tendrils of these plant-like animals, 
				feeding on small creatures that might harm the anemone. In turn, 
				the anemone helps protect the clownfish with its poisonous 
				tendrils, keeping larger predators away.<br>
				<br>
				This relationship is an example of mutualism, part of a broader 
				biological process known as symbiosis. The word, coming from two 
				Greek roots: syn meaning “with” and biosis meaning “together,” 
				describes a mutual relationship that develops between two 
				organisms. Sometimes parasitic and sometimes beneficial, 
				symbiosis describes a system in which the two organisms 
				contribute to the other’s support.<br>
				<br>
				The idea of symbiosis can be just as powerful in economics as it 
				is in biology. Rather than dealing with organisms, it can deal 
				with economic sectors. In Cambridge, Massachusetts, and Silicon 
				Valley, for example, a type of economic symbiosis has been 
				taking place in the past few decades. Long term investments in 
				education in both areas have helped build up tech-savvy 
				universities like Stanford and MIT.<br>
				<br>
				In recent years, these universities have been attracting and 
				creating cohorts of computer programmers and software designers, 
				who go on to found tech companies – Microsoft, Apple, Facebook, 
				Google and hundreds more. Meanwhile, the presence of successful 
				tech firms improves the quality of education by providing 
				university students funding, mentors and training. In short, 
				education nourishes a successful tech sector, which in turn 
				continues to give back to education.<br>
				<br>
				A look at recent economic investments in the Sultanate reveals 
				significant potential for similar types of economic symbiosis. 
				This concept is demonstrated in Oman’s energy sector, where 
				ambitions for higher oil and gas exports and more investment in 
				renewable energy are beginning to complement one another. Oman’s 
				hydrocarbon reserves are enough for the country’s own needs, but 
				long-term export contracts and an energy-hungry industrial 
				sector have meant that Oman still needs to import supplies at 
				times. As a result, companies are always on the lookout for ways 
				to squeeze every last drop of oil out of mature wells. One 
				method of coaxing difficult-to-reach oil out is flooding wells 
				with steam – steam which is typically generated by burning 
				natural gas.<br>
				<br>
				A California-based firm named GlassPoint has found a more 
				cost-effective way to create this steam: the sun. Using 
				low-cost, mirror-like solar collectors inside of glass shelters 
				(imagine a greenhouse), GlassPoint harnesses solar energy to 
				heat water and create steam for enhanced oil recovery (EOR). So 
				far the method is quite competitive. GlassPoint’s solar steam 
				costs $3.78 per million British thermal unit (mbtu). This 
				compares with gas, which goes for $5.78 per mbtu in the 
				company’s home market of California.<br>
				<br>
				Last August, GlassPoint announced that Petroleum Development 
				Oman, the Sultanate’s national oil company, awarded a contract 
				to build a 7MW solar collector. “After extensively researching 
				solar EOR solutions, we’ve identified GlassPoint as the most 
				promising technology for this pilot”, Syham Bentouati, PDO 
				Corporate Technology Advisor, said. “GlassPoint’s solar steam 
				generators have the potential to release valuable natural gas 
				for use in higher-value applications within the Sultanate.”<br>
				<br>
				This situation demonstrates an interesting case of economic 
				symbiosis. Oman’s investments in solar energy projects are, 
				ironically, helping to reduce costs of fossil fuel production. 
				Meanwhile, fossil fuel production is supporting the development 
				of new solar collection techniques. <br>
				<br>
				As in energy, investments in the Sultanate’s tourism sector have 
				been creating opportunities for economic symbiosis, too. Tourism 
				has long been a critical piece of Oman’s Vision 2020 economic 
				diversification plan. Over the past ten years, visitor numbers 
				have been rising steadily. The Sultanate had 1.52 million 
				international arrivals in 2009, according to the World Bank 
				Development Indicators, nearly double its 829,000 international 
				arrivals in 2001.<br>
				<br>
				By 2020, the Ministry of Tourism is aiming to attract 12 million 
				tourists annually. In order to reach that goal, more money is 
				being devoted to the sector. The country’s 8th five-year plan 
				(2011-15) earmarks $1.5bn for tourism projects. In coming years 
				investments in the sector are set to total up to $20bn, 
				according to Business Monitor International.<br>
				<br>
				A significant portion of these investments is going toward 
				all-inclusive tourism areas called integrated tourism complexes 
				(ITCs). Examples of ITCs include the $1bn Salalah Beach project 
				and the $400m Jebel Sifah project, both being carried out by 
				Muriya Tourism Development. The company, partially owned by the 
				Sultanate’s tourism arm Omran (30 per cent) and Egypt’s Orascom 
				Hotels &amp; Development (70 per cent), has planned for the resorts 
				to include hotels, restaurants, retail space and golf facilities 
				– in short, all of the anticipated needs of a typical tourist.<br>
				<br>
				By offering ways to accommodate growing numbers of visitors, 
				ITCs like Salalah Beach and Jebel Sifah provide a number of 
				opportunities for related sectors. For construction, ITCs could 
				lead to higher demand, as the complexes themselves would require 
				massive construction projects. Moreover, development in these 
				areas could lead to longer term growth thanks to aspects of the 
				Sultanate’s property law.<br>
				<br>
				According to a 2007 housing law, non-Omanis are allowed to fully 
				own and develop property in ITCs, making them an attractive 
				prospect for expatriates interested in second homes. By 
				enlarging the market for foreign buyers, ITC investments could 
				offer a boost to the construction sector. Meanwhile, as more 
				expats purchase vacation properties, tourism could benefit from 
				a steady stream of visitors coming to visit their properties.
				<br>
				<br>
				In retail as well, ITCs and other tourism investments seem to be 
				creating mutually beneficial conditions. Another ITC called 
				Omagine, for example, is set to integrate hotels, retail shops, 
				a boardwalk and a cultural theme park made up of seven 
				pearl-shaped buildings. The $2.5bn project’s attractions could 
				pull more customers into shops, offering the potential for 
				positive knock-on effects in the retail sector. On the other 
				hand, as retail options grow in the country as a whole, the 
				sector can help attract more tourists for shopping holidays, 
				sending visitor numbers even higher.<br>
				<br>
				The idea here is to invest in areas that can create these 
				positive feedback loops. To that end, progress is promising. 
				Areas like hydrocarbons and renewables are supporting one 
				another’s growth, which in the short- to medium-term could 
				contribute to holding energy prices down. At the same time, a 
				developing trifecta of tourism, construction and retail is 
				helping to diversify the economy away from oil and gas – a 
				long-standing goal of the Sultanate’s economic decision-makers.</font><p align="justify" style="margin: 3px 10px">
