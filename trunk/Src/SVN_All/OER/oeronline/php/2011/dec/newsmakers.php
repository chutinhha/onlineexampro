				<span style="font-size: 9pt">Oman has seen quite a few of its 
				leading personalities making waves either by winning awards, 
				promoting international ties or by simply standing up in their 
				line of duty<br>
				</span></span></font></strong><font face="Verdana" size="1"><br>
				<b><br>
				</b>
				<img border="0" src="images/newsmakers_dec1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="126" align="right"></font><b><font color="#000080" size="2" face="Verdana">Promoting 
				tourism</font></b><font face="Verdana" size="1"><br>
				Shaikh Abdulmalik bin Abdullah Al Khalili, Minister of Tourism<br>
				<br>
				Shaikh Abdulmalik bin Abdullah Al Khalili, minister of tourism, 
				was earlier the chairman of a leading bank in the Sultanate. HE 
				Khalili brings with him his dynamic leadership that 
				characterised his stint in the financial sector. In his new 
				responsibility as the minister of tourism, he has been involved 
				in a lot of activities. He launched a booklet under the theme 
				‘Tourism Linking Cultures’ to mark the World Tourism Day. The 
				booklet will be used for tourism promotions and as a resource 
				for students considering a career in tourism and hospitality 
				sectors. <br>
				The Ministry of Tourism also hosted the third Global Geotourism 
				Conference showcasing the earth’s amazing geology and landforms 
				and promoting awareness of geological heritage and sustainable 
				development of geological resources in Oman itself.With Muscat 
				being named as the 2012 Arab Tourism Capital, HE Khalili and the 
				ministry of tourism will have their hands full.<br>
				<br>
				<img border="0" src="images/newsmakers_dec2.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="128" align="right"></font><b><font color="#000080" size="2" face="Verdana">Digital 
				movement</font></b><font face="Verdana" size="1"><br>
				HE Ali Masoud Al Sunaidi, Minister of Sports Affairs and 
				Chairman of ITA<br>
				<br>
				HE Ali bin Masoud Al Sunaidi, Sports Affairs Minister has been 
				entrusted with the extraordinary task of heading the Information 
				Technology Authority (ITA) which he is doing with aplomb. All 
				his speeches delivered on behalf of ITA speak of his knowledge 
				on the subject and conviction about the digital strategy of 
				Oman.<br>
				HE Sunaidi points out that since the establishment of the ITA, 
				it has been responsible for implementing this strategy in 
				collaboration with the different governmental institutions. To 
				this end it implemented the unified governmental network, the 
				national Data Centre, Information Security Centre, Information 
				Safety Centre, E-Payment portal and the development of 
				e-government services. The value of software exports, products 
				and services amounted to around RO70mn or 2 per cent of the 
				gross non-oil exports. The internet service penetration rate 
				amounted to 74 per cent as of June 2011. As per the Census 2010, 
				54 per cent of the Omani families have computers. <br>
				<br>
				<img border="0" src="images/newsmakers_dec3.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="127" align="right"></font><b><font color="#000080" size="2" face="Verdana">Enterprising, 
				dynamic</font></b><font face="Verdana" size="1"><br>
				Hind Bahwan, Chairperson of Bahwan CyberTek and Director, Suhail 
				Bahwan Group<br>
				<br>
				Hind Bahwan, chairperson of Bahwan CyberTek Group and director 
				of the Suhail Bahwan Group, was conferred the prestigious ‘ABLF 
				WOMAN OF POWER AWARD’ by the Asian Business Leadership Forum – a 
				platform backed by a think-tank of prominent leaders, 
				policy-makers, academicians, media and opinion leaders of core 
				industries across Asia.<br>
				She is recognised in the Middle East as an enterprising and 
				dynamic businesswoman. She founded Bahwan CyberTek Group in 1999 
				which is now a leading provider of IT products and Services with 
				1450+ professionals in its workforce rendering products and 
				services in 20 countries across 4 continents. Bahwan CyberTek 
				has its centre of excellence in Chennai, India and presence in 
				the USA, Dubai, Egypt, Brunei, besides India. <br>
				As one of the directors of the multi national conglomerate the 
				Suhail Bahwan Group and founder of Bahwan CyberTek Group, Hind 
				plays a key role in working with the government and other 
				leading corporate entities in pursuit of creating a digital 
				society in Oman.<br>
				<br>
				<img border="0" src="images/newsmakers_dec4.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="125" align="right"></font><b><font color="#000080" size="2" face="Verdana">Flying 
				high</font></b><font face="Verdana" size="1"><br>
				HE Maithaa Al Mahrouqi, Undersecretary, Ministry of Tourism<br>
				<br>
				Maithaa Al Mahrouqi, a science graduate from the University of 
				Oxford Brookes, UK, started her career as an airlines executive 
				with Emirates Airlines, then moved to Gulf Air as its business 
				development manager and was subsequently promoted as the general 
				manager of Gulf Air for Oman. She then went on to become the 
				country manager of Oman Air. With a wealth of experience and 
				knowledge that she acquired within a span of a decade, HE Maitha 
				was destined for bigger roles in life. Her appointment as the 
				undersecretary of Ministry of Tourism is a testament to her 
				experience in the hospitality industry.<br>
				She also carries with her the tag of perhaps being the first 
				female cartographer from the region, and the first Omani 
				cartographer as well. HE Maitha’s exposure of working with major 
				airlines of the region and her vast knowledge of the tourism and 
				hospitality industry enables her to wield her influence and 
				competence in promoting the country as a big tourist 
				destination.<br>
				<br>
				<img border="0" src="images/newsmakers_dec5.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="125" align="right"></font><b><font color="#000080" size="2" face="Verdana">Cultural 
				ties</font></b><font face="Verdana" size="1"><br>
				Shaikh Mohammed Saud Bahwan, chairman, Saud Bahwan Group<br>
				<br>
				Shaikh Mohammed, who as the chairman of Oman-Japan Friendship 
				Association, took the lead in helping out tsunami-stricken 
				Japan. Seiji Morimoto, Ambassador of Japan to the Sultanate of 
				Oman, appreciated the gesture of the Oman Japan Friendship 
				Society.<br>
				Shaikh Mohammed has always been known for his philanthropic 
				activities and the initiative to help Japan is one of them.<br>
				Under the leadership of Shaikh Mohammed, the Saud Bahwan group’s 
				automotive division has been gaining new ground despite adverse 
				conditions in the global automotive market. His efforts have 
				been recognised by Toyota Motor Corporation which Marketing 
				Excellence Award to Shaikh Mohammed.</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1"><br>
				<br>
				</font><font face="Verdana" size="1">
				<img border="0" src="images/newsmakers_dec6.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="126" align="right"></font><b><font color="#000080" size="2" face="Verdana">Shura 
				surprise</font></b><font face="Verdana" size="1"><br>
				Ne’ma Al Busaidi, member, Majlis A’Shura<br>
				<br>
				Ne’ma Al Busaidi became the only woman to win the elections to 
				the seventh term of Majlis Al Shura from the Seeb wilayat. She 
				is the lone woman member in the 84-seat Consultative Council. 
				Ne’ma is a teacher at Al Mabeela Northern School in A Seeb area 
				and was a graduate from the Teachers’ College in 1988. She has 
				been teaching for 23 years. Her motivation to contest the 
				elections came from the fact she has been teaching for a long 
				time during which time she became close to students and their 
				parents and with the people in the community at large. She wants 
				to serve her country in a bigger way, to represent the people 
				and their needs to the government. Ne’ma says despite being the 
				lone lady in the council, she will represent women and the 
				issues facing them. </font>
