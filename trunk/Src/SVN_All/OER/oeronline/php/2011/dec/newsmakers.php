				Shaikh Saad Suhail Bahwan, Chairman of OTE Group and member of 
				Majlis A’Shura<br>
				<br>
				Shaikh Saad, the son of Shaikh Suhail Bahwan, was elected to the 
				Majlis A’Shura as member from Sur. He is expected to play a 
				greater role in that body. Shaikh Saad, one of the pioneers of 
				successful business establishments in the Sultanate, formed OTE 
				in a small way in 1990 and has taken the trading company to new 
				heights. The automobile division of OTE has recently witnessed 
				tremendous growth thanks to Shaikh Saad’s visionary moves to 
				invest in the facilities and brands of the division. The 
				companies that sprouted under the wings of OTE since then are 
				now firmly rooted.</font><p align="justify" style="margin: 10px">
