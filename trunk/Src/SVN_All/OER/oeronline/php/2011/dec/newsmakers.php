				different</font></b><font face="Verdana" size="1"><br>
				Ross Cormack, CEO, Nawras<br>
				<br>
				Ross Cormack has been named Outstanding Leader of the Year at 
				the third TMT Finance &amp; Investment Awards Middle East, held in 
				Dubai. This accolade for Oman’s customer friendly communications 
				provider follows a year in which Nawras became a public company 
				after the largest IPO in the Sultanate since 2005 and also began 
				offering broadband services to corporate and residential 
				customers.Cormack has led Nawras, a Qtel Group company, since 
				its formation in 2004. From the launch of customer-friendly 
				mobile services to today’s successful public company delivering 
				innovative mobile services and broadband internet services, 
				Nawras has remained true to its values of being caring, 
				excellent and pleasingly different. In March, Cormack received 
				the Strategic Leadership Award at the Global HR Excellence 
				Awards. <br>
				<br>
				<img border="0" src="images/newsmakers_dec9.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="126" align="right"></font><b><font color="#000080" size="2" face="Verdana">Country 
				first</font></b><font face="Verdana" size="1"><br>
				Dr Mohammed bin Hamad al Rumhy, Minister of Oil and Gas<br>
				<br>
				Dr Mohammed bin Hamad al Rumhy, Minister of Oil and Gas, has 
				been spearheading the sustainable development of the oil and gas 
				sector of Oman. HE Rumhy has also become the new chairman of 
				Oman LNG, the country’s only gas production facility and the 
				second biggest revenue earner for the country. Despite the 
				challenges in discovery of new oil and gas fields and some big 
				companies like BG pulling out of the Oman market, the ministry 
				continues to attract new firms to explore for hydrocarbons in 
				the country. The most significant development has been the 
				awarding of a licence to US-based international energy firm 
				PetroTel to explore for hydrocarbons in Block 40 offshore 
				Musandam Governorate in the north of the Sultanate. </font>
