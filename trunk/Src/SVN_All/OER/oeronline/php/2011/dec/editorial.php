						A bigger canvas</font></b><p align="justify" style="margin: 3px 10px">
				<b><font size="2" face="Verdana"><br>
				</font></b><font size="1" face="Verdana">A year back few would 
				have guessed that 2011 would turn out to be such a watershed 
				year for the Middle East. The self immolation of the 26-year- 
				old Mohammed Bouazizi in Tunisia, set in motion a wave of 
				protests across the region. The issues and concerns– jobs for 
				nationals, better standards of education, weeding out corrupt 
				officials and accountability – cut across geographical 
				boundaries.<br>
				<br>
				Taking about accountability, I would like to share a piece of 
				conversation with the CEO of a telecom company. The CEO 
				mentioned that the turn of events this year had trained the 
				spotlight firmly on the role of companies and businesses in 
				community welfare. In 1970 the Nobel Laureate Milton Friedman 
				wrote a famous article for The New York Times titled, ‘The 
				social responsibility of business is to increase its profits.’ 
				The principal argument of the article was that corporations add 
				far more to society by maximising long-term shareholder value 
				(profits) than they do by donating time and money to charity or 
				other causes. From an investor’s point of view, the purpose of 
				business is to maximise profits, but this seems to be a narrow 
				definition of corporate responsibility. <br>
				<br>
				The other school of thought is that business should have a 
				social conscience and companies should be concerned with 
				promoting desirable ‘social’ ends like providing employment, 
				eliminating discrimination, avoiding pollution etc. An 
				enlightened corporation should try to create value for all of 
				its constituencies -- customers, employees, suppliers, and the 
				community. Each of these group defines the purpose of a business 
				through the prism of its own needs and desires, and each 
				perspective is equally valid and legitimate. In the new milieu, 
				it is imperative that Corporate Oman proves itself to be up to 
				the task of promoting the larger interest of the community.<br>
				<br>
				On behalf of the entire team of OER, I would like to wish all 
				our readers a merry Christmas and a very Happy New Year.<br>
