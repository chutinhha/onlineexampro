						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Rising to the occasion</strong></span><p class="style15">
				<strong><span class="style14">The Public Authority for 
				Electricity and Water has drawn up ambitious plans to augment 
				the power and water capacities as the demand rises. HE Mohammed 
				bin Abdullah Al Mahrouqi, Chairman, PAEW highlights some of 
				these initiatives in this exclusive interview with Visvas Paul D 
				Karra</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-utlities1.jpg" style="border: 1px solid #808080; float: right;" class="style30"></font><strong>What 
				are the power projects that have been planned for the coming 
				years?<br>
				</strong>Additional electricity generation capacity will be 
				needed to meet the projected demand growth, which is currently 
				running at more than 8 per cent per year. The Sohar 2 and Barka 
				3 projects will provide about 1,300MW of capacity and are 
				already in train with expected commercial operation dates in 
				2012. The Salalah IWPP (450MW) is expected to be completed this 
				year. Beyond that, we expect the requirement for new capacity to 
				continue to grow and the procurement process is already in place 
				for a new IPP project at Sur, which will deliver between 1,500 
				and 2,000MW and at least 400MW of early capacity by the summer 
				of 2013. We are also developing IWPP projects in Musandam, 
				projected at 100MW.<br>
				<br>
				The expected rapid growth of demand in Al Wusta associated with 
				various large scale developments that are ongoing and those 
				planned around the new Duqm port will require significant 
				additional power and water capacity. Studies will shortly 
				commence to look at the appropriate size and timing options for 
				this and how this should be integrated with the rest of the 
				electricity system and other existing demands in the area. We 
				also have plans to introduce large-scale solar power generation 
				into the system in Oman and we expect the size and timing of 
				this to become clear in the next few months.<br>
				<br>
				<strong>What is the timeline for introducing renewable energy 
				into the electricity grid system of the Sultanate?<br>
				<br>
				</strong>A large scale 100-200MW solar energy system exploiting 
				our abundant natural resource is planned and we expect the 
				procurement process for this plant to begin in the first half of 
				2011.<br>
				<br>
				<strong>What are the policy instruments needed to support 
				implementation of renewable energy projects across the Sultanate 
				on a large scale?<br>
				<br>
				</strong>Our existing system is naturally designed around what 
				are termed “conventional” energy sources. Introducing renewable 
				energy on a large scale may require some changes to recognize 
				the substantially higher cost of renewable energy – at least at 
				present. While renewable sources of energy may be free, the 
				capital investment needed to exploit these is substantial and 
				investors will need assurance about returns while we need to be 
				sure that the costs of renewable energy are affordable within 
				the overall energy portfolio. It is envisaged however that our 
				first large scale solar plant will follow the well tested IPP 
				structure with some changes to cater for the nature of this 
				project.<br>
				<br>
				<strong>What is the status of the feasibility report for Oman’s 
				first large-scale solar plant and in what stage are the pilot 
				projects?<br>
				<br>
				</strong>The feasibility study for the proposed large-scale 
				solar plant is being completed and expected to be finished by 
				the end of the year. The Authority for Electricity Regulation 
				(AER) has shortlisted six pilot projects for approval. Two of 
				them are under finalisation of contracts between developers and 
				the Rural Area Electricity Company. The other four are under 
				further technical and commercial discussions/ clarifications.<br>
				<br>
				<strong>What are the plans of PAEW to cater to the rising demand 
				for water supply? What is the expenditure for developing new 
				water infrastructure?<br>
				<br>
				</strong>The demand for water is growing rapidly in the 
				Sultanate. This is driven both by the increase in population and 
				by improved living standards as well as by significant 
				industrial and commercial developments such as those around Duqm 
				in Al Wusta. We are developing a programme of capacity additions 
				to meet this growth and a number of these are already in hand 
				like the additional IWP capacity at Ghubra, which is needed to 
				meet the growing demand in the Muscat area. There is also a plan 
				to reduce reliance on groundwater by shifting to desalination as 
				the prime source of water to retain groundwater as a strategic 
				long-term reserve. We are also investing heavily in water 
				transmission and distribution networks, which provide additional 
				security of supply and allows for the use of larger centralized 
				desalination projects with consequent significant economies of 
				scale. Overall, we expect to invest around RO500mn on water 
				projects over the next five years; one element of our underlying 
				assumptions is that we will also make progress in reducing 
				losses and so-called ‘unaccounted for water’ as part of the way 
				we will deal with increasing demands.<br>
				<br>
				One of the key performance indicators for the new management 
				contract is the level of both real and apparent losses and we 
				expect these to reduce over time as systems and infrastructure 
				are improved.<br>
				<br>
				In this matter, PAEW has many projects to execute in different 
				regions and wilayats during the next few years covering pipeline 
				works (distribution and transmission) including reservoirs, 
				water sources and desalination.<br>
				<br>
				<strong>Can you give more details of the contract awarded to 
				Veolia Water to provide management services to the Sultanate’s 
				water sector?</strong><br>
				<br>
				The recently-awarded contract has the key objective of assisting 
				PAEW to build a ‘best practice’ public sector organization with 
				a hands-on approach to operations and management and to develop 
				our human resources and knowledge transfer. We expect to build a 
				strategic partnership with Veolia and not simply manage a 
				contract so that the benefits of the contract become embedded in 
				our organization. PAEW retains the option of forming a private 
				entity or maintaining public control of these activities.<br>
				<br>
				The management contractor’s scope includes the ongoing 
				‘co-management’ of the business and operations with PAEW leading 
				to improvement of the operational levels of service and 
				development of a strategic business plan for PAEW, including 
				procedures for updating the plans and maintaining its relevance.<br>
				<br>
				Veolia will assist PAEW with human resource development and 
				training, knowledge transfer, organizational restructuring and 
				change management and the development of strategies and plans 
				for different aspects of the business.<br>
				<strong><br>
				What are the new water projects being executed in the 8th Five 
				Year Plan?<br>
				</strong><br>
				There are many water projects, which are being executed under 
				the previous five year plan in different regions and wilayats. 
				The projects include network (distribution and transmission) 
				like small reverse osmosis (RO) plants. Some of the works 
				include:<ul class="style29">
							<li class="style28">
							<p class="style15">Muscat governorate: Execution of 
							larger RO plants</li>
							<li class="style28">
							<p class="style15">Extension of Barka plants</li>
							<li class="style28">
							<p class="style15">Sohar desalination plants</li>
							<li class="style28">
							<p class="style15">Transmission network to Dakhliyah 
							and Al Batina regions, A’Dahirah and Al Sharqiyah 
							</li>
							<li class="style28">
							<p class="style15">Sharqiyah desalination plants</li>
						</ul>
						<p class="style15"><strong>What is the timeframe for 
						restructuring and unbundling of the water sector?<br>
						</strong><br>
						The government has approved the award of a management 
						contract for five years as a first step prior to 
						restructuring the water sector. </p>
