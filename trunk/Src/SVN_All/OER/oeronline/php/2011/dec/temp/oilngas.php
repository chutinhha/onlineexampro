						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Time for optimism</span></strong><p class="style15">
				<font face="Verdana" size="1">
				<strong><span class="style14">As economic green shoots emerge, 
				the buoyancy in crude price continues. With the commodity now 
				breaching a 25-month high, analysts are cautiously optimistic of 
				a further up move. Sunil Fernandes reports</span></strong></font><p class="style15">
				As the global economic recovery gathers steam, the undertone in 
				crude continues to remain buoyant. The US Energy Information 
				Agency, the International Energy Agency (IEA) and OPEC, see 
				rising crude demand in 2011, riding piggyback on the global 
				economic recovery.<br>
				<strong><br>
				Marginal growth<br>
				</strong><br>
				In 
				2009, the world suffered from demand destruction, thanks to the 
				economic chaos that engulfed the world. Such was the mayhem that 
				it resulted in energy consumption not only being lower, but also 
				witnessing a decline for the first time since 1982. In 2010, it 
				was widely anticipated that demand figures would see a modest 
				growth and 2011 might not buck that trend.<br>
				<br>
				“In 2011, world oil demand growth is forecast at 1.2 million 
				barrels per day (mb/d), representing a minor upward revision of 
				50 thousand barrels per day. The continued pace of the global 
				economic recovery will have a considerable impact on world oil 
				demand in 2011,” OPEC stated in a recent report.<br>
				<br>
				The IEA expects global oil demand to expand by 1.4 million 
				barrels a day or 1.6 per cent year-on-year in 2011 to 89.1 
				million barrels a day. The agency believes that the OECD demand 
				is projected to reverse to its underlying, structural decline in 
				2011.<br>
				<br>
				<strong>Mixed economic data<br>
				</strong><br>
				Economic 
				activity and hence economic data, has a lot to do with where 
				crude prices would trend in the future. Towards the end of last 
				year and early this year, crude oil prices rallied to the 
				highest in 25-months fuelled by economic data emanating from the 
				US. In the latest signs of improvement in the US economic 
				recovery, data showed consumer sentiment rose more than expected 
				in early December, while import prices in November 2010 climbed 
				at their fastest pace in a year.<br>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-oilngas2.jpg" style="border: 1px solid #808080; float: right;" class="style28"></font>“U.S. economic growth could certainly trend higher in 2011, 
				although the moribund housing market will continue to act as a 
				heavy drag on any momentum. Employment numbers as of late have 
				been improving and the effect of the second round of qualitative 
				easing (QE2), both immediate and far into the future is a topic 
				of much debate. Any pick-up in U.S. economic growth (or even a 
				fairly flat 2011 vs 2010) should result in positive sentiment 
				towards crude prices,” says Jonathan Kornafel, director, 
				Asia/Europe, Hudson Capital Energy. <br>
				<br>
				Economic data from the US, Germany (European Power house), China 
				and India, all major consumers of oil rwemains optimistic. 
				German business confidence began the new year at record highs, 
				an eighth consecutive increase, while China’s GDP has surged to 
				10.3 per cent in 2010. In fact, the insatiable demand for oil 
				comes from growing economies like India and China, which are 
				expected to show robust GDP growth rates well into the future.
				<br>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-oilngas3.jpg" width="100" height="133" style="border: 1px solid #808080; float: right;" class="style28"></font>Eliane 
				Tanner, commodity strategist, Bank Sarasin &amp; Co, Switzerland, 
				however predicts a lackluster demand for crude amidst tepid 
				economic growth predictions. “Economic growth in the USA and 
				Europe is likely to slow down somewhat from the levels seen in 
				2010. Moreover, demand from those regions is structurally 
				weakening due to improvements in energy efficiency, stricter 
				environmental legislations and natural gas substitution. 
				Consequently, we do not expect demand growth from the USA and 
				Europe to be a major driver in the oil market in the coming 
				years,” she says.<br>
				<br>
				<strong>Mixed opinion on prices<br>
				</strong><br>
				While economic growth remains uninspiring in select economies, 
				crude oil prediction remains mixed. US banking giant Goldmans 
				Sach reported recently that their medium term forecast for oil 
				prices in 2012 would be “substantially higher” as global oil 
				stockpiles shrink and oil production tightens.<br>
				<br>
				“We continue to expect strong price appreciation for key 
				commodities and thus substantial aggregate commodity returns. We 
				continue to see differentiation across commodity fundamentals 
				and ultimately, relative performance,” the report also notes. 
				The investment bank sees oil prices at $100 per barrel in 2011.
				<br>
				<br>
				A recent string of stronger data and technically bullish signals 
				have some analysts believe that the market is likely to rally.<br>
				<br>
				Jonathan Kornafel, director, Asia/Europe, Hudson Capital Energy 
				is bullish on short term oil prices. “Front-month West Texas 
				Intermediate (WTI) or Brent contracts could certainly break 
				through $100 during 2011, although current fundamentals do not 
				justify such a move. A number of external factors could propel 
				the market higher for a short-term break above the century 
				mark,” he says. <br>
				<br>
				Tanner echoes a similar view and believes that crude can touch 
				the $100 mark. “In general, we see oil price hovering between 
				$80 to $100 in 2011. Nevertheless, we cannot rule out temporary 
				price movements above the $100 mark in 2011 when risk appetite 
				improves or geopolitical tensions in some critical regions flare 
				up,” she says. <br>
				<br>
				The US Energy Information Administration (EIA) expects the price 
				of WTI crude oil to average at about $93 per barrel in 2011, $14 
				higher than the average price last year. For 2012, EIA expects 
				WTI prices to continue to rise, with a forecast average price of 
				$99 per barrel in the fourth quarter of 2012. EIA’s forecast 
				assumes US real gross domestic product (GDP) grows by 2.2 per 
				cent in 2011 and 2.9 per cent in 2012, while world real GDP 
				(weighted by oil consumption) grows by 3.3 per cent and 3.7 per 
				cent in 2011 and 2012, respectively.<br>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-oilngas1.jpg" style="border: 1px solid #808080; float: right;" class="style28"></font>“With 
				oil prices today hitting the $90 mark, I won’t be surprised to 
				see prices touching the $100 mark as early as the second quarter 
				of 2011,” says Haitham Al Fannah, chief operating officer, Al 
				Sulaimi Group.<br>
				<br>
				A clear consensus on a more stable outlook on crude prices seems 
				to emerge, at least in the short term. However, if we take a 
				more long term view, it’s clear that the world would need a lot 
				of energy. Concerns of declining production particularly in 
				Russia (now the largest producer of oil and gas), sharp rise in 
				population and geopolitical threats would continue to see a 
				gradual spike in prices over the longer term.</td>
