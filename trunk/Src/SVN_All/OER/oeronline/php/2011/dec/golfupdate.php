				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo12.jpg" style="border-style: solid; border-color: #808080; float: right;"><b><br>
				You won the most promising golfer to be award at OER CEO Golf 
				2011. Can you share your views on winning this coveted 
				championship?</b><br>
				It was an ecstatic moment, it’s been ages since I won any 
				individual award in a sporting event. I am humbled and motivated 
				by the recognition and would like to credit UMS for 
				reinvigorating my sporting spirit. I believe OER CEO Golf is a 
				distinct event which blends an ideal cocktail of networking, 
				sporting and entertainment for corporate executives in Oman.<br>
				<br>
				<b>OER CEO Golf is considered as one of the premier networking 
				events for the business community of Oman where business is 
				mixed with pleasure. What is your opinion about OER CEO Golf as 
				an event?</b><br>
				OER CEO Golf has attained a reputation which is beyond 
				comparison and is gradually becoming a barometer for judging 
				successful professionals, an invitation for the event is treated 
				as a certification and recognition of the achievements of 
				corporate executives and getting labeled as an achiever. <br>
				<br>
				<b>OER CEO Golf will be held in January 2012. What will be your 
				expectations for this year? </b><br>
				Each year UMS has raised the bar by experimenting successfully 
				with the format of the event and as ever I expect UMS to beat 
				the expectation this time as well. OER CEO Golf has no one but 
				itself to compete with and I wish team UMS yet another success.<br>
				<br>
				<b>Your comments on the game of golf?</b><br>
				It’s a game which is quite unassuming , I always had an 
				impression that it is a lazy man’s game and doesn’t have 
				anything exciting in it, but now I beg to differ and have 
				concluded its a game which actually accentuates not just your 
				physical fitness but the mental faculty as well. It is the best 
				game to develop the skill sets befitting a corporate role. Golf 
				like any other sport requires a lot of hard work and patience to 
				learn and excel and isn’t as easy as it appears to be. It’s fun 
				and a great stress buster. <br>
