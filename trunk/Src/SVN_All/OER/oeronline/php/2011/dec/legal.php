						<p align="justify" style="margin: 3px 10px">
				<font color="#CC3333" size="4" face="Verdana"><b>Amendments to 
				the Labour Law<br>
				</b></font><font face="Verdana" size="1"><br>
				<font color="#808080"><i>By James Harbridge<br>
				</i></font><br>
				</font>
				<font face="Verdana" style="font-size: 9pt; font-weight: 700">
				The changes are quite far-reaching and it will be interesting to 
				see whether the amount of labour cases decrease as a result</font><font face="Verdana" size="1"><br>
				<br>
				<br>
				It has been well publicised that the Labour Law has now been 
				amended by Royal Decree 113/2011.<br>
				<br>
				The aim has been, in part, to try to clear up areas of doubt. 
				For instance, employers are now obligated to pay salaries within 
				seven days of the due date. Also, the new Law makes it clear 
				that the 30 days’ annual leave can be taken after the expiry of 
				six months from the joining date.<br>
				<br>
				Emergency leave on full pay has been increased from four to six 
				days per year.<br>
				<br>
				<b>Other important changes include the following:</b><br>
				<br>
				The maximum working hours per week has been reduced from 48 to 
				45 hours, and Muslim employees in Ramadhan will now work 30 
				hours, rather than 36 hours/week.<br>
				<br>
				Article 70 stipulates that an employee must now grant two 
				consecutive days of rest (on full pay; ie on gross salary) per 
				week.<br>
				<br>
				An area of ambiguity about overtime for work performed on 
				weekends and official holidays has now been resolved. The old 
				reference to a “double wage” to be paid for such work has now 
				been replaced with an unambiguous clause which says that the 
				doubling would apply to the basic salary, rather than to the 
				gross salary.<br>
				<br>
				Article 81 used to say that women could not be required to work 
				from 7pm to 6am. The Law now changes those timings to 9pm – 6am.<br>
				<br>
				Maternity rights are now enhanced. Female employees have the 
				right, three times during their service with the respective 
				employer, to a 50 day maternity leave on full pay.<br>
				<br>
				The Law also states that a Ministerial Decision will be issued 
				to set procedures and control mechanisms in respect of private 
				sector establishments which violate the Labour Law.<br>
				<br>
				Furthermore, each employer who does not comply with the 
				stipulated Omanisation percentages will pay a fine of not less 
				than RO250, and not more than RO500, in respect of each Omani 
				employee which should have been employed. The employer will, 
				additionally, be required to rectify the Omanisation percentage 
				within six months from the date when the violation was detected. 
				The fines would be doubled if there is then a repeat of the 
				violation.<br>
				<br>
				In conclusion, the changes are quite far-reaching and it will be 
				interesting to see whether the amount of labour cases decrease 
				as a result. It is clear that employers will be under close 
				scrutiny.<br>
