						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>
						<a name="TYERS">Retire in peace</a></strong></span><p class="style15">
				<font face="Verdana" size="1"><strong><span class="style14">
				i2Offshore has launched a financial product that caters to the 
				growing demand for a pension and savings scheme that will 
				complement the existing PASI schemes and end of service payments 
				for expatriates. Visvas Paul D Karra reports<br>
&nbsp;</span></strong></font><p class="style15" style="text-align: center">
				<img border="0" src="images/face2face-july-1.jpg" width="200" height="202" align="left" style="border: 2px solid #C0C0C0"><p class="style15">
				As with any rapidly emerging nation with a diverse and 
				increasingly dynamic economy, planning for retirement is 
				becoming a growing issue in Oman. Countries around the world are 
				facing a mounting struggle to provide their citizens with the 
				rewards in retirement they would like to have, which means that 
				companies within the public and private sector now have to look 
				at introducing their own pension and savings schemes to plug 
				this growing gap.<br>
				<br>
				I2Offshore has come up with a proposal which gives an 
				opportunity for an organisation to create its own bespoke and 
				branded state-of-the-art corporate pension/savings scheme. It 
				has been created to ensure the implementation of the most 
				contemporary on-line financial systems in a pension/savings 
				scheme that would benefit all employees at every level of 
				employment and of any nationality helping to ensure the 
				long-term welfare of an organisation’s whole workforce in 
				today’s changing and challenging business environment.<br>
				The Muscat-based i2Offshore has recently launched this unique 
				new pension and savings product that will bring significant 
				benefits to employers and individual employees – both Omani 
				nationals and expatriates.<br>
				<br>
				“The new product is the first in Oman that gives individuals the 
				opportunity to select schemes that can be genuinely 100 per cent 
				capital-protected for the full amount of the investment, are 
				highly flexible in how they are structured and have the security 
				of being backed by a highly respected London-based pension 
				specialist company,” says Barry Woodgate, senior consultant, 
				i2Offshore.<br>
				<br>
				Traditional bank and insurance-based schemes tend to be 
				equity/bond based and no longer offer the flexibility required 
				to ensure that investments are maximised and managed. If the 
				global equity and or bond markets are having a period of 
				volatility – so is your pension. Therefore, i2Offshore believes 
				that to maximise the impact, the company corporate 
				pension/savings scheme needs to be able to respond more quickly, 
				have a more flexible multi-asset approach with the flexibility 
				to make investment decisions at a minute’s notice on a truly 
				global scale.<br>
				<br>
				<font color="#000080"><b>Employer’s responsibility</b></font><br>
				<br>
				Woodgate explains that this scheme will not take the place of 
				Public Authority for Social Insurance (PASI) but will act as a 
				complement to it. As an example if an individual is earning 
				RO1,000 per month, in retirement from PASI within the private 
				sector they will receive approximately RO350 per month. 
				According to Woodgate, this means that without making sound 
				provisions individuals will be worse off in retirement.<br>
				<br>
				“This is why we feel that an implemented company pension/ 
				savings scheme will bring great benefit to companies and the 
				individual employees with added bonus of bringing huge benefits 
				to Oman’s retirement structure, as PASI’s percentage is becoming 
				significantly lower year on year. This can help to plug the 
				growing gap and to help individuals to ensure that they have the 
				savings in place to enjoy the fruits of their retirement, says 
				Woodgate.<br>
				<br>
				Currently under the terms of the Social Insurance Law, the PASI 
				will pay social service benefits to Omani employees who have 
				subscribed to the national insurance scheme and have properly 
				invested in the scheme. The private employers are required to 
				make 10.5 per cent monthly contributions towards the PASI fund 
				while employees and the Omani government contribute the 
				requisite balance into the insurance fund. <br>
				<br>
				At the end of their service, Omani employees are entitled to 
				receive social service benefits based on their last drawn 
				aggregate basic salary.<br>
				<br>
				In addition, as per Article 39 of the Omani Labour Law, 
				expatriate employees are generally entitled to the end of 
				service gratuity payment on the termination of their employee 
				contract.<br>
				<br>
				<font color="#000080"><b>Benefits of the scheme</b></font><br>
				<br>
				One of the key benefits of the i2Offshore system can be seen in 
				recruitment, retention and rewarding of senior executives, and 
				in general staff recruitment. With a scheme like this, new 
				employees will view the organisation as having a visionary 
				outlook in looking after their future. This corporate 
				pension/savings scheme will encourage staff loyalty and make 
				them think twice before considering other career options and 
				senior staff will have more rewards through the scheme.<br>
				<br>
				Generally, employers are recognising the value from an employee 
				engagement perspective, of providing end-of-service indemnities. 
				Retirement or savings plans can also be an effective way to 
				enhance employee engagement and at the same time potentially 
				reduce liabilities associated with indemnities. <br>
				<br>
				<b><font color="#000080">Individual investment strategy<br>
				</font></b><br>
				The scheme will reimburse the individuals benefits/ savings back 
				to the company within 24 hours if an employee leave or retires. 
				If an employee leaves they can take their benefits/saving at the 
				time of their departure – no lock up period – offering truly 
				flexibility. And there is full on-line reporting and 24x7 
				internet access via a secure password and log-in.<br>
				i2offshore believes that companies who adopt the scheme make 
				themselves more attractive to talented staff in an increasingly 
				competitive labour market in the country — and also help in the 
				longer-term retention of Omani nationals and expatriate 
				employees.<br>
				<br>
				<b><font color="#000080">Different accounts</font></b><br>
				<br>
				i2Offshore says that the product has already generated 
				significant interest among the corporate sector in the Sultanate 
				with many leading Omani organisations already signing up to the 
				product and offering it to their employees. The scheme has been 
				set up in a way that individual companies can brand the product 
				as their own – giving a stronger ownership and sense of 
				belonging to the organisation and its staff.<br>
				<br>
				The beauty of the platform is that an employer can create a 
				scheme that can match the profile of their workforce and meet 
				the employee benefit strategy. Within the scheme will be the 
				availability to individuals/members to each to be able to have 
				their own specific investment strategy-giving clients the option 
				to control investments.<br>
				<br>
				<b><font color="#000080">Full protection</font></b><br>
				<br>
				Explains Woodgate, “A key differentiator in our product is 
				having trustee partners in the form of Brooklands Trusteeswho 
				are regulated by the FSA/Bank of England, who operate out of 
				offices in the City of London and Dubai and a financial nominee 
				in the shape of Brown Shipley Private Client Bank, a FTSE 100 
				company in London. Having the pensions and savings in trust 
				ensures the individual’s savings is fully protected.<br>
				<br>
				“The product is already generating interest among Omani 
				businesses, and we believe this will be a real added benefit to 
				companies in their recruitment strategies in recruiting and 
				retaining senior executives and staff, helping them to be seen 
				as visionary in their approach to the financial security of 
				their employees,” Woodgate says.<br>
				<br>
				<b><font color="#000080">Investment flexibility</font></b><br>
				<br>
				Woodgate added that another important asset for companies was 
				giving them control of the investment strategy working with 
				ourselves with a recommended approach for companies to adopt 100 
				per cent capital structured products that could give a growth 
				return of 6-17 per cent per annum.<br>
				<br>
				“We wanted to create a product that keeps companies away from 
				the volatility of local or global equity and bond markets – 
				especially in light of the recent global financial downturn from 
				which no one in the world has remained immune,” he added.<br>
				<br>
				The scheme offers multi asset class investing, with asset 
				allocation of bonds both government and corporate, hedge funds, 
				precious metals of gold, silver and platinum, commercial 
				property, equities, commodities, currencies, private equity, 
				cash. i2Offshore believes that diversification and managing the 
				correlations within the client’s portfolio’s investment strategy 
				is absolute key.<br>
				<br>
				i2Offshore is an independent financial consultancy that has been 
				operating in Oman for the past 15 years and also has offices in 
				Kuwait and Baku and is part of the Tamimah Consultancy Group, 
				led by Shaikh Mohammed al Harthy, an aide to the cabinet of the 
				government of Oman. </td>
