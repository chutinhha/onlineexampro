						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Learn from best practices</strong></span><p class="style15">
				<span class="style14"><strong><i><font color="#808080">Ashar M. 
				Nazim</font></i>, Director, Islamic Financial Services and <i>
				<font color="#808080">Abid Shakeel</font></i>, Financial 
				Services/Advisory, Ernst &amp; Young, Bahrain were in Muscat to 
				address an Islamic Banking Seminar. Mayank Singh talks to them 
				about the roadmap and potential of Islamic Finance in Oman</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/fainance-july-1.jpg" width="200" height="199" style="border: 1px solid #808080; " align="left" hspace="40"><img border="0" src="images/fainance-july-2.jpg" width="200" height="199" style="border: 1px solid #808080; " align="left" hspace="4"></font><p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				&nbsp;<p class="style15">
				<b>Q: The government has permitted Islamic Finance (IF) in Oman, 
				what kind of business opportunity does this convert into?<br>
				</b><br>
				<b>Nazim:</b> The best way is to look at other markets in the 
				GCC in terms of what IF has done for them and as Oman is very 
				similar to these markets one can expect a similar result here 
				too. We have seen that a majority of banks have asked for IF as 
				a desirable feature, if not a deal breaker. This means that done 
				rightly -- with the correct regulatory framework, the requisite 
				talent pool and the right infrastructure, we believe that upto 
				60 per cent of the banking market would like to look at Islamic 
				Banking (IB) as a preferred option. Oman has a GDP of $50bn and 
				banking deposits of $42bn and we are talking about a 8-10 per 
				cent market share over the next five years which translates into 
				a $6bn IB industry on the retail side. The other opportunity is 
				on attracting FDI, wherein Oman can emerge as a destination of 
				choice for a number of IF investments. Large scale projects can 
				look at having an IF tranche. So the country as a whole will 
				have access to a new funding pool. Thirdly, it will promote an 
				inclusive financial system and that would mean that a lot of 
				SMEs which have chosen to stay away from conventional banking or 
				a few retail customers who have stayed away from microfinance or 
				the insurance industry as these were seen as being unIslamic 
				will now avail of such opportunities. The global IF industry is 
				around one trillion dollars and despite the financial crisis it 
				is growing in double digits.<br>
				<br>
				Shakeel: Experience in other markets shows that IF grows rapidly 
				after an incubation period of five years. Saudi Arabia, UAE and 
				Kuwait are examples of how a 10 per cent growth rate gains 
				traction after five years. From the customers perspective all 
				things being equal – by which we mean products, availability, 
				distribution, pricing, infrastructure and a guarantee that these 
				products are genuinely Sharia compliant – the population in GCC 
				countries would strongly prefer Islamic products. IF will 
				continue to grow in double digits as more countries come on 
				stream and as excess liquidity in the Middle East multiplies. IF 
				is poised to become more of a global offering than just being a 
				niche offering.<br>
				<br>
				<b>Q: Now that the government has given the nod to IF, what 
				needs to be done from a regulatory point of view and 
				institutions?<br>
				</b><br>
				<b>Nazim:</b> The regulator needs to come out with a policy 
				statement on regulating full Islamic banks and IB windows of 
				conventional banks. This will be followed by a full-fledged 
				regulatory framework that defines the risks, governance, capital 
				adequacy and liquidity requirements of Islamic banks. I would 
				say that Oman has a chance to take a leadership role in this 
				field, although it is the last amongst GCC countries to allow 
				IF, it can learn from others and think in terms of taking IB to 
				the next level. <br>
				<br>
				<b>Shakeel:</b> There are different methodologies for the 
				regulator to follow and the Central Bank’s role would be to 
				understand as to what is happening in other jurisdictions and 
				then take a decision on what is appropriate for them. Putting in 
				place the best regulatory regime would make Oman attractive not 
				only for financial institutions and foreign players who want to 
				do business here, but also for consumers.<br>
				<br>
				<b>Q: One of the criticisms levelled against IF is that unlike 
				conventional banking IF is open to interpretation as there is no 
				single model to follow. How can Oman insure that it stays clear 
				of such problems?</b><br>
				<br>
				<b>Nazim:</b> What you are saying was the case 20 years ago, 
				since then global industry setting bodies have come up. For 
				example the global standard setting body for Islamic accounting 
				standards is Accounting and Auditing Organisation for Islamic 
				Financing Institutions (AAOIFI). It has 45 member countries and 
				200 different Sharia scholars from different schools of thought 
				and markets, moreover it has a buy in from all the markets. On 
				the regulatory side there is Islamic Financial Services Board (IFSB), 
				which is headquartered in Malaysia. It has 300 members, like 
				commercial banks, investment banks etc, bodies like BASEL, world 
				bank, Islamic Development Bank, and these institutions have 
				converged on a platform to develop a regulatory framework for 
				the industry. Oman should align itself with these industry 
				setting bodies and then customise according to its specific 
				needs. This will ensure local relevance and class leading 
				international best practices.<br>
				<br>
				<b>Q: IB also requires certain changes in commercial law, so 
				that banks can own underlying assets. How long do such changes 
				take?<br>
				</b><br>
				<b>Nazim:</b> There are a number of different underlying 
				contracts for different financial products, it is not always 
				necessary for the bank to buy these products like a house or a 
				car and all this is a part of the learning process. The 
				regulators too are going through the same journey as the 
				financial institutions and there will be regulatory changes 
				which will be introduced but it will not be as dramatic as 
				everybody is expecting. From a product perspective people need 
				to understand that there are variations there.<br>
				<br>
				<b>Q: IF bucked the global financial meltdown to a large extent 
				as it did not have as much exposure to collateral debt 
				obligations (CDO’s) and real estate as conventional banks. Is 
				this a major plus for IF?</b><br>
				<br>
				<b>Nazim:</b> The key strength of IF is that it is linked to the 
				real economy. It is not a financial bubble like the conventional 
				global financial system where only nine per cent of the money is 
				linked to the real economy and everything else is exotic 
				liquidity. IF has to be asset backed. All transactions have to 
				be asset linked either to a lease, equity or trade. However IF 
				has to mature and during the financial crisis we saw that banks 
				which were focussed on their core banking business did well, but 
				others who had large exposure to real estate got burnt badly.<br>
				<br>
				<b>Shakeel:</b> The basic principles of banking still apply if 
				you have assets they should be spread across various categories. 
				IF is still in its early learning phase and this was the first 
				crisis that it faced. What we are seeing in the last two years 
				is that a recovery in asset prices has helped IB which had 
				substantial real estate exposure to recover in equal measure, 
				and having learnt a lesson most of the banks are trying to 
				rationalise their real estate assets. IB is not just for Muslims 
				but it comes close to ethical banking or socially responsible 
				banking and it is as appealing to non Muslims as Muslims. 
				Ethical banking means that the roles and responsibilities of 
				various parties have to be balanced, the money is invested in 
				permissible activities etc.<br>
				<br>
				<b>Q: How do you see IF or IB shaping up in Oman in the coming 
				years? <br>
				</b><br>
				<b>Nazim:</b> IB is not going to be a walk in the park for Oman 
				as there is a lot of skepticism worldwide whether what is being 
				offered is Islamic or not. Banks will have to address four key 
				elements. One, they will have to understand their customers 
				better, issues like whether they are happy with an Islamic desk 
				within a conventional branch or do they expect a standalone 
				branch where their money is separate from a conventional bank. 
				Second, the regulator has to ensure that there are qualified 
				Sharia compliant officers and a Sharia Board which can guide in 
				the interpretation of the Sharia. Third, banks need to invest in 
				enhancing their operational capabilities as Islamic and 
				conventional businesses need to be kept separate and accounted 
				for separately. Finally a regulatory regime has to be in place 
				which is in consonance with international bodies and standards.<br>
