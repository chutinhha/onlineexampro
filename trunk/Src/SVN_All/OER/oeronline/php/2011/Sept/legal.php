						<p class="style15">
				<b><font face="Verdana" size="4" color="#C00000">How to stay 
				away from disputes</font></b><p align="justify" class="style13">
						<p align="justify" class="style13">
				<br>
				<b><font size="2">The more careful you are the lesser will be 
				the possibility of your company being dragged into litigation .
				<font color="#808080"><i>BY James Harbridge</i></font><br>
&nbsp;</font></b><p align="justify" class="style13">
				<br>
				Quite naturally and understandably, any business person wishes 
				to avoid disputes and court cases wherever possible. Here are 
				seven ways in which you can reduce the possibility of your 
				company being involved in litigation. <br>
				<br>
				<b><font size="2" color="#C00000">Exclusions</font></b><br>
				<br>
				When making a quote for goods and services, it is often good to 
				be specific about what is not included in the quote. The more 
				clarity there is about what the quote does and does not include, 
				the greater is the chance there is that a dispute will not 
				occur. In addition, words in a quote often need to be defined so 
				that there can be no doubts and conflicting interpretations 
				between the parties.<br>
				<br>
				<b><font size="2" color="#C00000">A fresh pair of eyes</font></b><br>
				<br>
				It makes sense to get a worker unconnected with a bid to review 
				that bid after it has been put together by others. There is more 
				of a chance of errors being spotted by someone who has not been 
				toiling over one document for a number of hours. This is 
				particularly true if the document is being amended from an 
				earlier bid document. There is always a danger that if people 
				are working under stress and time pressure – old terms and 
				conditions are left out. A fresh pair of eyes is a great safety 
				check for a company.<br>
				<br>
				<b><font size="2" color="#C00000">Supervision</font></b><br>
				<br>
				The word “Supervision” made in an offer can be dangerous as you 
				may wrongly be held responsible for more than you should be. The 
				word “Supervision” should always be caveated to say what the 
				supervision pertains to, and it is best also to state any limits 
				on such supervision.<br>
				<br>
				<b><font size="2" color="#C00000">Risks</font></b><br>
				<br>
				Sometimes a client may ask you to try to limit your quote (i.e. 
				to make it cheaper!). Sometimes, a lower price can mean a 
				greater risk in some respect for the customer. It is always best 
				to have an evidential paper trail which shows that you have 
				fully warned the customer in writing of the risk they will be 
				taking if they accept the cheaper price.<br>
				<br>
				<b><font size="2" color="#C00000">A customer’s lack of 
				understanding</font></b><br>
				<br>
				It can happen that you make something explicitly clear in 
				writing to a customer, and then, in later correspondence, they 
				intentionally or unintentionally state a position which is 
				inconsistent with what you have explicitly told them earlier. In 
				such a scenario, it is always best to repeat and reiterate in 
				writing that which you have told them earlier. The risk is that 
				your silence may otherwise be perceived by the courts as your 
				consent to a change of position/different state of affairs.<br>
				<br>
				<b><font size="2" color="#C00000">A state of affairs on a given 
				day</font></b><br>
				<br>
				It can be important, on certain occasions, to photograph, say, a 
				construction site on a given date so that, at a future time, you 
				can prove what the position was at that earlier time. Such 
				photographs should be emailed the very same day to the other 
				party and, ideally, should also be delivered to them, with 
				signed, stamped and dated acknowledgement of receipt being 
				obtained.<br>
				<br>
				<b><font size="2" color="#C00000">A lot can happen in a day</font></b><br>
				<br>
				Sometimes there is a great pressure and your contractual 
				position with a client, especially in a crisis, may alter 
				dramatically in the course of one working day. This is when a 
				company can be exposed – when there is no paper trail to show 
				why concessions were granted, and for how long. For this reason, 
				it is always best – at the end of the day – to write back to 
				your counter-party, commenting on the decisions that were 
				agreed, and why they were agreed, and the duration for which 
				they will remain in force. <br>
