						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>CENTRE OF EXCELLENCE</strong></span><p class="style15">
				<b><font size="2">MARISOL has carved a niche for itself in the 
				medical tourism arena in Oman by facilitating access to world 
				class research and treatment facilities in Cuba. An OER report
				</font></b>
						<p class="style15" style="text-align: center">
				<img border="0" src="images/tourism01may11.jpg" style="border: 1px solid #808080"><p class="style15">
				<b>MARISOL</b> which promotes Tourism and Medical Tourism to 
				Cuba, has established its presence in Oman by providing ground 
				breaking, cutting edge technology and research through its 
				association with Cuba. Since its inception three years ago, the 
				company has successfully established its presence on the medical 
				landscape in Oman. This is amply reflected in its ever-expanding 
				client base ranging from the key ministries, government 
				personnel, clients from the private sector as well as the 
				general public in Oman.<br>
				<br>
				Unlike other organisations, MARISOL is the direct representative 
				of medical institutions in Cuba. Cuban medical services are 
				world-renowned and have pioneered the health movement globally 
				with the sole commitment to improving the quality of life 
				worldwide.<br>
				<br>
				Humanitarian missions in 68 countries are manned by 25,000 Cuban 
				doctors, and its medical teams have assisted victims of the 
				Tsunamis and the earthquakes in Pakistan and Haiti. Cuba has 
				already offered the services of its doctors and nursing teams to 
				aid the victims of the recent earthquake and Tsunami in Japan.<br>
				<br>
				<b>Helping Haiti</b><b><br>
				</b>Cuba was the first to come into Haiti with medical aid when 
				the January 12, 2010, earthquake struck. Among the many donor 
				nations, Cuba and its medical teams have played a major role in 
				treating Haiti’s earthquake victims. Public health experts say 
				the Cubans were the first to set up medical facilities among the 
				debris and to revamp hospitals immediately after the earthquake 
				struck. <br>
				<br>
				Haiti and Cuba signed a medical cooperation agreement in 1998. 
				Before the earthquake struck, 344 Cuban health professionals 
				were already present in Haiti, providing primary care and 
				obstetrical services as well as operating to restore the sight 
				of Haitians blinded by eye diseases. More doctors were flown in 
				shortly after the earthquake as part of the rapid response.<br>
				<br>
				Although Cuba is a poor, developing country, their wealth of 
				human resources—doctors, engineers, and disaster management 
				experts—has enabled this small Caribbean nation to play a global 
				role in health care and humanitarian aid alongside the 
				far-richer nations of the west. Cuban medical teams played a key 
				role in the wake of the Indian Ocean tsunami and stayed the 
				longest among international medical teams treating the victims 
				of the 2006 Indonesian earthquake. <br>
				<br>
				They also provided the largest contingent of doctors after the 
				2005 earthquake in Pakistan. In the Pakistan relief operation, 
				the US and Europe also dispatched medical teams. Each had a base 
				camp with most doctors deployed for a month. The Cubans, 
				however, deployed seven major base camps, operated 32 field 
				hospitals, and stayed for six months.<br>
				<br>
				Last year alone, 1,800 doctors from 47 developing countries 
				graduated in Cuba, with these numbers rising rapidly. During the 
				1990s, Cuba became the first country to develop and market a 
				vaccine for meningitis B. Then there was a surge in exports of 
				its hepatitis B vaccine, which is currently being shipped to 30 
				countries, including China, India, Russia, Pakistan and Latin 
				American countries. <br>
				<br>
				Successful clinical trials in several countries have already 
				established <br>
				Cuba as a world leader in cancer <br>
				<br>
				research and treatment. Globally recognised as a frontrunner in 
				medical research and services, Cuban experts and specialists 
				have excelled in the field of biotechnology, medicinal drug 
				production and Cancer treatment.<br>
				<br>
				Medical services in Cuba provide a result oriented set of 
				programmes aimed at the physical and psychological well being of 
				patients. These specialised programmes include the 
				rehabilitation programme, surgery programmes and retinitis 
				pigmentosa programme which was created by Dr. Orfilio Pelaez and 
				has been acknowledged as the most unique of its kind in the 
				world of medicine. Other treatments include ophthalmology, the 
				neurological restoration programme, ozone therapy, orthopedics, 
				psoriasis, vitiligo and alopecia programmes.<br>
				<br>
				A testament to Cuba’s global impact in the world of Medicine 
				lies in the fact that they are present throughout clinics and 
				missions in Europe, Latin America, Africa, Asia, Russia and the 
				Far East. Cuban doctors are actively involved in healthcare 
				missions for the World Health Organisation and their expert 
				services are in high demand in specialised medical organisations 
				worldwide.<br>
				<br>
				According to Marisol Cruz Fernandez, executive director, Cuba 
				has been recognised globally as the pioneer in the treatment of 
				skin diseases such a Vitiligo, commonly known as Leucoderma, 
				psoriasis and alopecia, acute baldness brought on by fungal 
				infection of the scalp. <br>
				<br>
				<b>The focus of Marisol’s medical </b><br>
				tourism programme includes ophthal- mology, orthopaedics and 
				medical rehabilitation. Giving further details, Marisol says: 
				“Our comprehensive medical tourism package covers visa issue, 
				travel documents, accommodation, local tours, transportation and 
				all other related activities. <br>
				<br>
				<b>Cutting edge care<br>
				</b>In 1986, the huge international success of the pioneering 
				Cuban method for the treatment of Vitiligo, (Leucoderma) with a 
				medicine obtained from human placenta, prompted the Cuban 
				government to set up the Placental Histotherapy Center headed by 
				Dr Carlos Miyares Cao for further research. <br>
				<br>
				Prior to this in the 1970s Cuba used to annually export 40 
				tonnes of human placenta to Meriux Laboratories, in France under 
				a commercial agreement. However, around this time, Dr Carlos 
				Miyares Cao, a physician specialist in gynecology, obstetrics 
				and pharmacology, and a professor from Medical School of the 
				University of Havana, Cuba, discovered Melagenia, a substance 
				that stimulated skin pigmentation activity, while studying live 
				placental metabolism under laboratory conditions. <br>
				<br>
				After the successful use of that substance in the treatment of 
				Vitiligo or Leucoderma, Cuba decided during 1980-82 to 
				discontinue the export of placenta to France and to begin, 
				within the National Pharmaceutical Industry, the production of 
				the new medicine for distribution in pharmacies on medical 
				prescription. According to Marisol, in 1985, the first 
				international service of Vitiligo patients was created at the 
				Cira Garcia Clinic, in Havana City, under Dr Carlos Miyares Cao. 
				The tremendous response from nearby countries like Mexico, 
				Venezuela and Colombia etc, led to the establishment of the 
				country’s first health tourism programme through medical 
				tourism. <br>
				<br>
				In 1987, the International and National Clinical Service for 
				Vitiligo patients was set up to cope with the increasing number 
				of foreign patients requesting to be treated in Cuba with the 
				new medicine (100 patients monthly, from more than 90 
				countries), as well as to treat Cuban patients suffering this 
				disease throughout the country.<br>
				<br>
				Consequently, there were centres at the Canary Islands (Spain), 
				Republic of San Marino, Madrid, (Spain); Brazil; Bogota, 
				(Colombia); Mexico City, (Mexico); Lima, (Peru); Nicaragua; El 
				Salvador; Entre Rios, (Argentina); Kiev, (Ukraine); Moscow and 
				Petro grad (Russia); Kirghiz; Kazakhstan; Riga; Latvia and 
				Nigeria, (Africa). <br>
				<br>
				The success of Marisol is driven by the single-minded objective 
				of providing a harmonious balance of genuine medical care with 
				globally recognised research and treatment for the well being of 
				people in Oman.<br>
				<br>
