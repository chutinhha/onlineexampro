						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Realty business models</strong></span><p class="style15">
				<span class="style14"><strong>A number of players in the real 
				estate industry have adopted the business model of vertically 
				integrating and participating on all legs of the value except 
				for designing By <font color="#808080"><i>sudhakar reddy</i></font><br>
&nbsp;</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/realstate1-sep2011.jpg" width="450" height="301" style="border: 1px solid #808080; " hspace="40"></font><p class="style15">
				<br>
				In any industry, different players adopt different strategies 
				depending on their strengths. Thus one can draw a strategic map 
				of an industry and find that different players occupy different 
				positions on the map. The real estate industry in Oman is no 
				different.<br>
				<br>
				The two variables on which a strategic map can be drawn are size 
				and the degree to which each player participates in the various 
				legs of the value chain. The value chain itself can be drawn as 
				follows<br>
				<br>
				There are companies that participate on all legs of the value 
				chain except perhaps designing and there are industry players 
				who outsource everything and only fund the project. There are 
				even players who just buy a property after is it completed and 
				let out.<br>
				<br>
				Integrated Tourism Projects (ITP) are a good example of players 
				participating in sourcing the land, preparing a design brief, 
				funding it and selling it. The actual construction, which is 
				labour intensive and requires deep core competencies in the case 
				of large projects, is outsourced. The designing is also 
				outsourced since it gives access to the best architects in the 
				business. Large projects although they are not ITPs have a 
				similar business model.<br>
				<br>
				A number of players have adopted the business model of 
				vertically integrating and participating on all legs of the 
				value except possibly designing. Here again returns are 
				attractive but only if there are significant competencies a 
				player possesses in all parts of the value chain. This business 
				model, which is a successful one, has a size limitation as very 
				large projects cannot be developed on this model owing mainly to 
				the fact that construction is labour intensive and cannot be 
				scaled up or down with time. Thus players who have adopted this 
				model are small and medium size property developers.<br>
				<br>
				Another strategy is simply to buy properties after they are 
				built and leased. The advantage of this model is that most of 
				the risks are eliminated since the project is completed and 
				leased out. However, lower risks go with lower returns. Pension 
				funds follow this model although some of them have also been 
				developing their own properties in order to get higher returns.
				<br>
				<br>
				The returns depend on the degree of vertical integration but 
				more importantly on the degree of competence a player has in the 
				parts of the value chain that it has decided to participate in. 
				This is critical and must be understood clearly. Players with a 
				similar business model can end up with vastly different returns 
				depending on the degree of competence they have in their chosen 
				activities.<br>
