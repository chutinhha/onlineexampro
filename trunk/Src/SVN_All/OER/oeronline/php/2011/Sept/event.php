						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Oman Debate 2011 on 
						October 31</strong></span><p class="style15">
				<strong><span class="style14"><font size="2">The annual 
				integrated business symposium encompasses key themes that form 
				the business environment of today and tomorrow in Oman</font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event-sep2011.jpg" width="400" height="330" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>Oman Debate 2011, the third annual national debate 
						organised by Oman Economic Review, will be held on 
						October 31.<br>
						<br>
						The hot topic of discussion for Oman Debate 2011 is how 
						to cope up with the developments of the Arab Spring, 
						which has had its repercussions on the Sultanate of 
						Oman. The leadership of the Sultanate of Oman has been 
						forced to go back to the drawing board to re-chart its 
						future course of action following the pro-reforms 
						movement which began in Oman as well.<br>
						<br>
						The government has reworked on its priorities and 
						policies and has taken measures to address various 
						issues. This, in turn, has raised a number of 
						questions/concerns among both the public and the public 
						sectors. <br>
						<br>
						<font color="#C00000"><b><font size="2">Some key 
						questions which will be raised by Oman Debate 2011 
						include the following:</font></b><font size="2"> </font>
						</font><br>
						<br>
						<b>Job creation:</b> Is creating 50,000 jobs in one year 
						the solution to the unemployment problem or are there 
						structural issues like changes in the education system, 
						making people employable, training etc that need to be 
						looked at. <br>
						<br>
						<b>Raising salaries</b>, sops and benefits: Can the 
						corporate sector foot the bill? Is the private sector 
						being made to foot an unsustainable bill or has the 
						corporate sector failed to do its part in the 
						development process? <br>
						<br>
						<b>Omanisation:</b> Are these realisable goals or 
						unrealistic expectations? The government has set 
						Omanisation targets for various sectors but the private 
						sector is struggling to find suitable/employable Omanis 
						to fill those positions.<br>
						<br>
						<b>At the crossroads:</b> Is this a watershed moment in 
						Oman’s socio-economic history where everything that was 
						held as sacred needs to be questioned and redefined?<br>
						<br>
						The last two editions of annual integrated business 
						symposium had witnessed packed audiences totalling over 
						400 movers and shakers from Oman’s corporate world, all 
						innovators in their profession converging to witness the 
						intense indepth discussions on the state of the economy.<br>
						<br>
						The 2010 debate featured key panelists from various 
						business sectors cutting across the industry who shared 
						rivetting views and incisive questions about how Oman’s 
						corporate world should shoulder the responsibility of 
						complementing the government’s efforts to take the 
						country forward on its growth path.<br>
						<br>
						Oman Debate 2011, is being brought to you by Oman 
						Economic Review (OER) in association with the Capital 
						Market Authority (CMA) and will continue to maintain its 
						status as an unique platform where influential 
						individuals will share stories of best practices, 
						effective business case-studies and, most importantly, 
						attributes of true leadership. <br>
						<br>
						The event will cover globalisation and economics, human 
						capital, motivation and diplomacy with a strong focus on 
						the future of Oman through the idea of a new age of 
						leadership in a changing business landscape and economic 
						trends. <br>
						<br>
						As in the previous two editions, Oman Debate 2011 will 
						feature a number of ministers, dignitaries and members 
						of Oman’s corporate world.<br>
