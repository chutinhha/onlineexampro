				<span class="style5"><strong>With his well-grounded and 
				extensive knowledge of automobile industry, Chris Edwards, 
				General Manager, Dhofar Automotive, is looking at steering the 
				new company to new heights. A report by <i>
				<font color="#808080">Muhammed Nafie<br>
				</font></i>
				<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/bey1-sep2011.jpg" style="border:2px solid #C0C0C0; float: right" class="style4" width="200" height="240"></font>When 
				the Zubair Automotive Group and Shanfari Automotive joined hands 
				to form a new company – Dhofar Automotive – to distribute and 
				sell American car brands Chrysler, Jeep and Dodge (CJD) in the 
				Sultanate, in 2010, it was an executive with roughly 25 years of 
				experience in automobile industry who took the helm of the new 
				company. Chris Edwards, general manager of Dhofar Automotive, 
				has been in the industry since 1987 when he started his career 
				in the United Kingdom. He had worked for a number of brands 
				there including Toyota, Lexus, Ford, Mercedes Benz etc. and 
				served as general manager in multiple locations before he moved 
				to the Gulf region six years ago. His first assignment in the 
				region was with Ebrahim K. Kanoo in Bahrain where he led the 
				operation of its multi franchise distribution for three years. 
				Then he came to Oman to manage CJD brands on behalf of Shanfari 
				Automotive in the Sultanate till the new company came to 
				existence in 2010. <br>
				<br>
				The joint venture was a major boost to the automotive industry 
				in the Sultanate because prior to it both the companies 
				separately distributed and sold American brands throughout the 
				country. “Since the inception of the new company, we have become 
				fully independent. We have done research on both the brands and 
				the market. Particularly we have seen record sale every month 
				since January 2011. Ramadan market has been phenomenal,” says 
				Edwards. “Since this is a different company in which two 
				families are involved, customers have a lot of confidence. In 
				addition, we have launched several products offering a lot of 
				opportunities for the customers to choose from CJD brands as an 
				alternative.”<br>
				<br>
				<font color="#CC3333" size="2"><b>Products based on merit</b></font><br>
				<br>
				He believes that the market is very strong with customers 
				becoming more educated. “Since people are well educated, they 
				are choosing products on the basis of merit rather than 
				considering their history, making American and European brands 
				more popular as opposed to traditional Japanese vehicles,” he 
				adds. <br>
				<br>
				As a new company Dhofar Automotive is looking to piggyback on 
				the strength and impressive growth of Chrysler which has 
				undergone huge changes with its tie-up with Fiat which owns 52 
				per cent of the company. “The profit registered by the company 
				in the first half of this year is six year ahead of the business 
				plan it has submitted to the American government. Moreover, in 
				an initiative which demonstrates the company’s continuing 
				financial and operating improvement, Fiat bought the US 
				government’s 6 per cent stake in Chrysler, paying the US and 
				Canadian government loans, six years ahead of schedule,” he 
				avers.<br>
				<br>
				The recent roadshow by Dhofar Automotive which visited seven 
				cities in nine days helped the company reach out to its 
				prospective customers in the interior regions of the country. 
				Edwards says the show attracted a high turnout in Sohar, 
				Musannah, Ibri, Nizwa, Ibra, Sur and Salalah creating an 
				excellent opportunity for people to interact with the senior 
				management team of the company, including himself. <br>
