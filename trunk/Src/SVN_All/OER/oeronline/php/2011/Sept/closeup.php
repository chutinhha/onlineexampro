						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Return of inflationary pressures<br>
						<br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px">
				<img border="0" src="images/closeup1-sep2011.jpg" width="450" height="225" style="border-style:solid; border-color:#C0C0C0; "><p align="center" style="margin: 3px 10px">&nbsp;<p align="justify" class="style13">
				<span class="style14"><strong>Steady oil prices together with 
				the declining value of the US dollar are rekindling fears of 
				renewed inflationary pressures in the GCC region
				</strong></span><font color="#808080"><i>Dr Jasim Husain Ali</i></font><br>
				<br>
				Inflationary pressures are returning to the Gulf Cooperation 
				Council (GCC) economies, albeit in a shy way. Undoubtedly, 
				inflation poses a big challenge to the prosperity of the region 
				affecting those who live and work here.<br>
				<br>
				The average rate of inflation is expected to hover around 4 per 
				cent in 2011, up from 3 per cent in 2010 — partly reflecting 
				imported inflation. Still, the average rate could reach up to 5 
				per cent in 2011 on the back of steady prices of imported goods. 
				This is only the average, as rates tend to be higher in some 
				economies than others. <br>
				<br>
				Undoubtedly, inflation rates are no way close to those witnessed 
				a few years ago. Inflation was the order of the day in the GCC 
				economies in 2007, reaching as high as 14 per cent in Qatar and 
				11 per cent in the UAE. The unpleasant state was the consequence 
				of rising oil prices coupled with the declining value of the 
				American currency. Interestingly enough, the same phenomenon is 
				being repeated though not exactly in the same magnitude.<br>
				<br>
				<b><font size="2" color="#C00000">Oil factor </font></b><br>
				<br>
				Interestingly enough, steady oil prices together with the 
				declining value of the US dollar are rekindling fears of renewed 
				inflationary pressures in the GCC region. Not surprisingly, 
				relatively high — or in this case steady — oil prices result in 
				price hikes for other commodities, including food. Countries 
				that export farm products tend to raise prices in relation to 
				the steady rise of imported oil prices. The practice increases 
				inflationary pressures in the GCC economies, which in turn are 
				known for being net importers of food.<br>
				<br>
				Oil prices are projected to average $112 per barrel in 2011, up 
				from around $100 per barrel in 2010 and around $90 per barrel in 
				2009. Thus, oil prices are expected to remain steady on the back 
				of relatively strong global economic confidence.<br>
				<br>
				In retrospect, oil prices reached $147 per barrel in July 2008 
				but then dropped to nearly $40 per barrel by the year-end. The 
				fall reflected changing global sentiments following the collapse 
				of the subprime market in the US. Yet, the eruption of the 
				financial crisis caused other problems, including confidence 
				problems for stock markets and the real estate sector. <br>
				<br>
				Undoubtedly, the petroleum sector is the key to the GCC 
				economies. Saudi Arabia alone produces nearly 12 per cent of 
				global oil imports — a fact that helps make the kingdom the 
				largest oil exporter in the world. Still, other GCC countries 
				are noted for their exporting capabilities, notably the UAE and 
				Kuwait, in turn boosting global oil output by 3.3 per cent and 
				3.1 per cent, respectively.<br>
				<br>
				The rising oil prices strengthen treasury income in the GCC 
				countries, especially as regional economies tend to assume 
				relatively low oil prices whilst preparing governmental budgets. 
				For instance, Qatar assumed a notably low average rate of $50 
				per barrel in its 2011-2012 fiscal system starting in April. 
				Stronger income allows for firmer spending, certainly an 
				inflationary practice.<br>
				<br>
				<b><font size="2" color="#C00000">Dollar decline</font></b><br>
				<br>
				Concurrently, the value of the US dollar is under pressure 
				reflecting heavy consequences associated with a heavy debt of 
				$14trn. Fears of possible payment problems undermine the 
				dollar’s value. The greenback has reportedly lost more than 10 
				per cent of its value against the euro in the first half of 
				2011.<br>
				<br>
				GCC currencies, with the exception of Kuwait, fully peg their 
				currencies to the US dollar as part of a conscious choice. 
				Kuwait ties its dinar to a basket of currencies including the 
				dollar and Euro. Still, the dollar reportedly accounts for up to 
				70 per cent of all currencies in the basket. The link serves to 
				provide confidence in regional economies and gives investors an 
				opportunity to do business — notwithstanding fluctuations in 
				exchange rates.<br>
				<br>
				On the other hand, drawbacks include the practice of imported 
				inflation. The lower value of the dollar translates into higher 
				prices of imports from currencies not pegged to the greenback. 
				The GCC economies partly depend on Eurozone countries and China 
				for considerable amounts of their imports, including vehicles 
				and machinery. By one account, imported inflation was 
				responsible for as much as 30 per cent of overall inflationary 
				pressures that GCC economies faced in 2007 and 2008.<br>
				<br>
				By and large, home-grown farm solution is not viable due to some 
				hard facts-scarcity of fertile land and water shortage. It is 
				suggested that only 10 per cent of lands in GCC countries are 
				arable, primarily located in Saudi Arabia. According to Gulf 
				Research Centre, production of a tonne of barley requires some 
				1,212 cubic metres (42,801 cubic feet) of ground water reserves 
				in Saudi Arabia. Certainly, this is not wise use of a scarce 
				resource.<br>
				<br>
				<b><font size="2" color="#C00000">Cost of living</font></b><br>
				<br>
				Inflationary pressures partly explain different rankings of 
				major GCC cities in Mercer’s Worldwide Cost of Living survey for 
				2011. The recently released report ranks Abu Dhabi as the most 
				expensive city in the GCC for expatriates to live in, followed 
				by Dubai, Riyadh, Manama, Kuwait City, Doha, Muscat and finally 
				Jeddah. The survey depends on the outcome of some 200 items 
				which include housing, transport, food, household goods and 
				entertainment.<br>
				<br>
				It is rightly suggested that inflation is the worst enemy in any 
				economy, more so than unemployment. While inflation hurts to 
				varying degrees, the case is not as harsh with respect to 
				unemployment.<p align="center" class="style13">
