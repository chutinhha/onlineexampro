						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Anchoring the way</strong></span><p class="style15">
				<strong><span class="style14">Nico Vincart, Project Manager, Jan 
				De Nul Dredging speaks about the company’s role in building and 
				upgrading of the Duqm port complex. A report by Visvas
				<font color="#808080"><i>Paul D Karra</i></font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<font face="Verdana" size="1">
				<a target="_blank" href="images/casestudy1big-aug.jpg">
				<img border="0" src="images/ports1-sep2011.jpg" width="150" height="195" style="border: 1px solid #808080; " align="left"></a></font><p align="justify" class="style13">
				Duqm’s development as a new industrial destination hinges on the 
				shape of the port facilities, which include a crude oil export 
				terminal and a strategic storage facility, a free trade zone and 
				a downstream industrial area in addition to an airport, port, 
				dry dock, commercial and residential areas and plans for tourism 
				development. Thanks to its proximity to the busy regional 
				sea-lanes traversing Oman’s coastal waters, Duqm is being 
				conceived as a main maritime gateway that will serve as an 
				ambitious industrial and commercial hub.<br>
				<br>
				For the shipping industry, one of the most exciting things that 
				is happening on this side of the Arabian Sea is the giant dry 
				dock facility, which was opened for operations by Oman Dry Dock 
				Company (ODC) in March this year. Most notably, the dry dock 
				received its maiden customers namely two ships of Jan De Nul 
				Dredging (JDN), the Belgium-based dredging company which, 
				incidentally, has been instrumental in literally paving the way 
				for the construction of the dock. This historic achievement was 
				celebrated in Duqm at a grand ceremony held under the auspices 
				of HE Said bin Hamdoon Al Harthy, Undersecretary of the 
				Transport and Communications Ministry for Ports and Marine 
				Affairs, in the presence of Sheikh Sultan bin Mohammed al Nuaimi, 
				Wali of Duqm and other dignitaries of the Al Wusta Region and 
				members of ROP.<br>
				<br>
				Nico Vincart, project manager, JDN, Oman Branch, commenting on 
				this accomplishment said, “As we have been involved in 
				construction of a part of the ship repair yard, we were honoured 
				to be the first customers of the dry dock. We would like to 
				thank the ministry of transport and communications (MOTC) and 
				ODC for this unique opportunity.”<br>
				<br>
				<b><font size="2" color="#CC3333">Port project</font></b><br>
				<br>
				Oman’s Ministry Of Transport And Communications awarded a 
				consortium of companies, which included Consolidated Contractors 
				Company (CCC), Sezai Turkes – Feyzi Akkaya Ltd (STFA) of Turkey, 
				and JDN the contract for the maritime works for Duqm’s new port 
				in April 2007. The scope of works specifically for JDN consisted 
				of the deepening works of approach channel together with the 
				reclamation works behind the newly built quay walls.<br>
				<br>
				All works are well ahead of schedule with only 20 per cent of 
				the material yet to be dredged and 10 per cent of the sand to be 
				reclaimed. The main part of the port and the entrance channel 
				has now been dredged and the works at the ship repair yard and 
				the commercial quay are fully completed.<br>
				<br>
				Subsequently, the ministry of transport and communications also 
				awarded the expansion work of the Duqm port complex at an 
				additional cost of RO335mn to the same CCC-STFA-JDN consortium. 
				The earlier contract of RO187mn together with the new contract, 
				scales up to around RO522mn for the integrated marine works 
				infrastructure. The investments will go towards extending the 
				breakwaters and deepening the harbour basin and entrance channel 
				in addition to the already existing works for construction of 
				breakwaters, quay walls, as well as a substantial dredging and 
				reclamation component.<br>
				<br>
				The lee breakwater was also realigned and pushed out further 
				into the sea, while the main breakwater has been lengthened, 
				thereby creating a considerably expanded port basin. As such, 
				the total length of the breakwaters has grown from the original 
				5.3 kms originally to 7.7 kms under the revised plan. The most 
				significant impact will be on the main breakwater, which will 
				now boast 2.25 kilometres of quay wall for commercial berths, 
				from an earlier quay length of 700 mtrs. The commercial berths 
				will be equipped to handle container, grain, cement, commodity, 
				and a range of dry bulk carriers.<br>
				<br>
				<b><font size="2" color="#CC3333">Dredging works</font></b><br>
				<br>
				The scale of the dredging work, which began in 2007 and will be 
				fully completed in 2012, is huge. Vincart gives some 
				mindboggling figures: Around 70 million cubic metres of soil 
				needs to be dredged and around 15 million cubic metres of sand 
				has to be reclaimed behind the quay walls, as it will be very 
				big with two breakwaters. And then you have the quay walls, the 
				commercial quay, 2.2 kms long and the government quay, which is 
				1200mtrs. For the shipyard, it will be 3 kms of quay and the two 
				graven docks (dry docks) will be 410mts long each.<br>
				<br>
				The classic form of drydock, properly known as graving dock, is 
				a narrow basin, usually made of earthen beams and concrete, 
				closed by gates or by a caisson, into which a vessel may be 
				floated and the water pumped out, leaving the vessel supported 
				on blocks. The keel blocks as well as the bilge block are placed 
				on the floor of the dock in accordance with the “docking plan” 
				of the ship.<br>
				<br>
				At the moment, five JDN vessels are doing the dredging work 
				non-stop. Recently, one new cutter suction dredging ship arrived 
				from Croatia to join the fleet of ships. This new vessel has 
				three big split hopper barges. And then the company also has 
				trailing suction hopper <br>
				dredgers on site.<br>
				<br>
				Explaining about the dredging operations, Vincart says that the 
				cut soil is put in the barges and taken to an offshore 
				disposable area. There is a pre-identified natural pit in the 
				sea just off Duqm and JDN is filling up this pit with the soil. 
				For the land reclamation, sand is brought from 40kms off shore. 
				This is good quality sand, which is dredged and brought to the 
				shore in the hopper to reclaim the land behind the quay wall. 
				Thus, JDN has first built the breakwater and then the quay wall 
				and in between is the water 10 mtrs deep approximately. “We have 
				put 10 million cubic metres of sand behind that to make a nice 
				quay wall that is also ready now. In May 2012, we will hand over 
				the complete port to the ministry of transport and 
				communication,” he says.<br>
				<br>
				The port is already open for the dry dock operations. Commercial 
				vessels began to arrive since October 2010. Some GIB cranes 
				arrived for the dry dock from China. And in the dry dock, JDN 
				barges were docked for three days. Some of the dry dock 
				operations, which were carried out on JDN vessels, included 
				cleaning of the hull for sea growth and then polishing of the 
				propellers and maintenance of the engines. Effectively the dry 
				dock is fully functional.<br>
				<br>
				JDN is part of the Jan De Nul Group, which was founded in 1938 
				by Jan Frans Jozef De Nul as a civil construction company. 
				Responding to the market opportunities at that time, the De Nul 
				family accepted its first dredging work in 1951. Since then 
				there has been no looking back. <br>
				The activities of Jan De Nul Group are based on three pillars: 
				dredging activities, civil construction and environmental works. 
				Jan De Nul Group has the world’s most modern and most 
				technologically advanced dredging fleet at its disposal. In 
				2009, Jan De Nul Group reached a turnover of 2.1bn euros (69 per 
				cent dredging activities, 18 per cent offshore projects, 11 per 
				cent civil works and 2 per cent environmental projects).<br>
				<br>
				People and vessels. In a nutshell, that is the driving force 
				behind Jan De Nul Group. Thanks to the 5,000 employees and its 
				ultramodern fleet, today the group ranks at the top of the 
				international dredging and marine related industry. Also with 
				regard to civil engineering and environmental works, the group 
				is one of the largest contractors in Europe.<br>
