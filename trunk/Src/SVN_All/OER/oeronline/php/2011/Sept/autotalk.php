						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A pathbreaking effort<br>
&nbsp;</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-sep.jpg" style="border: 1px solid #808080" class="style4" width="450" height="228"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>The Range Rover Evoque is one of the most eagerly anticipated 
				launches of 2011. Mayank Singh test drives the SUV in Liverpool, 
				UK to see whether it lives upto the hype and excitement</b></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				Range Rover Evoque – the new mini-sport utility vehicle – is the 
				smallest, lightest, most fuel-efficient vehicle Land Rover has 
				ever built. The Evoque is one of the most innovative and 
				interesting cars to come out from Land Rover since the original 
				Range Rover 41 years ago. What’s more, it’s also one of the most 
				desirable. <br>
				<br>
				The Evoque is aimed at a younger, more fashion-conscious 
				audience than that which the company traditionally targets. Land 
				Rover says that 90 per cent of Evoque buyers will be new to the 
				brand and younger, more urban and a lot more female than its 
				traditional customers. The owners will love the Evoque as much 
				for its origins 67 years ago, when Maurice Wilks sketched the 
				first Land Rover in the sand at Red Wharf Bay in Anglesey, as 
				for its striking looks derived from the LRX 2008 Detroit show 
				concept. The Evoque’s authenticity matters even if it is aimed 
				at a new generation of urbanites. <br>
				<br>
				Drive this car anywhere and heads will turn. Most makers like to 
				think there’s a buzz behind their launches, but on this one 
				there is, with an unprecedented 18,000 orders already. Gerry 
				McGovern’s design team has done a wonderful job. From the 
				glittering lamps to the rally-like wheel arches and rear 
				spoiler, the Evoque exudes crispness and desirability. <br>
				<br>
				</font>
				<font face="Verdana" size="1" class="style11">
				<font color="#CC3333" size="2"><b>Exterior and interior</b></font></font><font face="Verdana" size="1" class="style11" style="font-size: 11px"><br>
				<br>
				It’s gorgeous, aggressive, contemporary, coupé-like styling is 
				extremely faithful to the LRX concept that was unveiled in 2008, 
				which is a pleasant surprise, since most road cars are toned 
				down dramatically by the time they reach production. The 
				Evoque’s interior is comfortable, well appointed and, if you opt 
				for the panoramic glass roof, light and airy.<br>
				<br>
				The delight continues in the cabin, with glistening switches and 
				dials from pricier Range Rover models and beautifully 
				upholstered and supportive seats in the front which, with the 
				adjustable steering column, give a perfect driving position. 
				Features from a higher grade of cars include one-touch interior 
				lighting and the split-screen centre console display, which 
				allows passengers to watch television or DVDs while the driver 
				can see only the satellite navigation.<br>
				<br>
				The chassis has been carefully wrought to make the most of its 
				Freelander-derived independent suspension. The swaying induced 
				by a tall ride height is partly assuaged by savings in top 
				weight and unsprung mass, with an aluminium bonnet, roof and 
				suspension components, and composite plastics one-piece 
				tailgate. Weighing in at 1.64 tonnes (plus 62lb for the optional 
				panoramic glass roof), the Evoque is 220lb less than the 
				Freelander, which also helps to reduce fuel consumption and CO2 
				emissions. <br>
				<br>
				</font><font class="style11">
				<font color="#CC3333" size="2" face="Verdana"><b>Drive quality
				</b></font></font>
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				<br>
				Within moments of setting off, it becomes obvious that the 
				Evoque drives as well as it looks. In motion, it feels every bit 
				as luxurious as its larger brethren, cruising comfortably 
				regardless of speed. It’s smooth and well-mannered around town 
				and remarkably quiet when hurtling along motorways. The Evoque 
				handles well, too. Throw it into a corner and you can’t fail to 
				be impressed by its vice-like grip – even in wet, treacherous 
				conditions. It behaves in a very coupé-like manner, rewarding 
				drivers who push it to the limit.<br>
				<br>
				This car is surprisingly capable off road, too. It displays an 
				impressive range of abilities, scaling hills of up to 45 
				degrees, dredging through up to 500mm of water, and generally 
				getting itself through situations that would leave ordinary road 
				cars stranded.<br>
				<br>
				The Evoque has the power to make the most of its excellent 
				suspension. Two engines are available, the first being a 
				2-litre, turbocharged petrol unit that produces 240bhp and 340Nm 
				of torque. The engine, available with a six-speed automatic 
				gearbox and four-wheel-drive transmission only, is potent enough 
				for a 7.1-second 0-60mph time and a 135mph top speed, which 
				feels more than quick enough from the Evoque’s raised driving 
				position.<br>
				<br>
				Land Rover quotes combined fuel economy of 40.9mpg, while CO2 
				emissions are rated at 199g/km, although Land Rover plants trees 
				to offset this for the first 45,000 miles of the vehicle’s use. 
				Land Rover also provides the option of a 2.2-litre diesel engine 
				with a choice of six-speed automatic or six-speed manual 
				gearbox, and two- or four-wheel drive transmissions. The most 
				eco-friendly of these options is the eD4 2WD manual model, which 
				returns 62.8mpg and CO2 emissions of 133g/km. <br>
				<br>
				The Evoque’s dynamic handling comes courtesy of an optional 
				adaptive dynamics suspension-damping system. It also feels 
				completely different to drive, similar to a car in response but 
				with an off-roader’s driving position. At medium-fast speeds it 
				rides firmly, but is supple and positive over bumps. There’s 
				enough suspension travel to shrug off most potholes, but it’s 
				asking a lot of a single set of dampers to provide off-road 
				agility and 100mph swerve safety combined with stability and 
				ride comfort.<br>
				<br>
				Land Rover’s engineers have managed pretty well even with the 
				standard steel suspension, the off-road ability is compromised 
				by a lack of suspension travel and the electronic traction but 
				hill descent features make up for a lack of mechanical traction. 
				The electronic power-assisted steering is almost brilliant but 
				for occasionally variable weighting. The brakes are powerful, 
				with a linear pedal feel. Driven hard, the Evoque is fun, safe 
				and more rewarding than anything in its class. You can also opt 
				for the Magnaride adjustable dampers, which extend the Evoque’s 
				competence and sporting capabilities across a wider range of 
				road types. All in all, the Range Rover Evoque is fabulous fun 
				to drive both on and off road.</font></td>
