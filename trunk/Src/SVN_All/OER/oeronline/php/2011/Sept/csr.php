						<span class="style27"><strong>Thinking out of THE box</strong></span><p class="style15">
				<strong><span class="style14">‘The Box Appeal’, a one month 
				charity campaign from August 15 to September 15 by Radisson Blu 
				and Park Inn hotels, seeks to help over 10,000 people across the 
				Middle East. <font color="#808080"><i>Muhammed Nafie reports</i></font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/csr-sep2011.jpg" width="450" height="169" style="border-style:solid; border-color:#C0C0C0; "><p align="justify" class="style13">
						<br>Radisson Blu and Park Inn hotels in Muscat launched 
						for the first time in Oman ‘The Box Appeal’, a one-month 
						long charity campaign aiming to help the less privileged 
						in the society. The successful charity campaign which 
						began in 2007 as ShoeBox Appeal in the UAE has been 
						re-branded for 2011, as it aims to help more people than 
						ever before with the participation of all Radisson Blu 
						and Park Inn hotels in the UAE, Oman, Egypt and Bahrain. 
						Now in its fourth year, the campaign from August 15 to 
						September 15 seeks to help over 10,000 people across the 
						Middle East. <br>
						<br>
						“As many as 13 hotels from across the MENA region are 
						expected to take part, with the two hotels in Muscat 
						aiming to fill 1,000 boxes collectively, helping to 
						ensure this is the most successful edition of the appeal 
						to date,” says Michael Jacobi, general manager, Radisson 
						Blu, Muscat.<br>
						<br>
						He says the campaign initially started by the Radisson 
						Blu hotel in Dubai Media City in 2007 in order to 
						provide labourers across the UAE with every day 
						necessities. It was the growing success of ShoeBox 
						Appeal in Dubai that made Rezidor Hotel Group to take 
						the campaign region-wide. Over the four years the 
						campaign has donated more than 20,000 boxes so far.<br>
						<br>
						“The campaign encourages people to pick up a small box 
						from the Radisson Blu or Park Inn hotels in the city or 
						from City Centre Muscat or Qurum City Centre, the Box 
						Appeal partners, and fill them with a list of everyday 
						items such as toothpaste, toothbrush, comb, towel, body 
						soap etc. The hotels have partnered with local charity 
						Dar Al Atta’a who will distribute the boxes at the end 
						of the campaign to families and individuals most in 
						need,” explains Jacob. He also added that many other 
						local companies have pledged their support to the 
						campaign in an astonishing show of community spirit.<br>
						<br>
						Rabih Zein, the recently appointed general manager of 
						Park Inn Muscat states, “The campaign reflects the Park 
						Inn philosophy of responsible business. We have been 
						actively involved in a plethora of charity activities 
						and social service programmes such as blood donation and 
						marathon collection. We encourage people across Oman to 
						come forward and collect, fill and return the boxes in 
						the true spirit of giving. Most of us hardly give a 
						thought to these everyday commodities but the boxes will 
						make a difference to someone for who even the basics 
						count as luxuries.” <br>
						<br>
						“We hope the simplicity of The Box Appeal campaign will 
						draw the attention of the public. Anyone can do this – 
						it is so easy to get involved and contribute,” adds 
						Jacob. The hotels have partnered with various companies 
						in the country in order to promote the campaign and 
						raise awareness about <br>
						it in society.<br>
