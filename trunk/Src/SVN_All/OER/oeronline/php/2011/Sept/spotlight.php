						<p class="style15">
				<b><font face="Verdana" size="4" color="#C00000">Be the sentinel 
				of your heart</font></b><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<img border="0" src="images/spotlight1-sep2011.jpg" width="400" height="411" style="border: 2px solid #C0C0C0"><p align="justify" class="style13">
						<p align="justify" class="style13">
				<br>
				<b><font size="2">World Heart Day is commemorated on September 
				29 every year since 2000 to inform people around the globe that 
				heart disease and stroke are the world’s leading cause of death, 
				claiming 17.1 million lives each year</font></b><p align="justify" class="style13">
				<br>
				You probably know someone – in your family or among your friends 
				– who has had a heart attack or a stroke (a brain attack). The 
				cardiovascular diseases – diseases of the heart and the blood 
				vessels – are killing more and more people around the world, 
				striking rich and poor alike. Those who survive a heart attack 
				or stroke often need to take long-term medical treatment.<br>
				<br>
				If you have ever had a heart attack or stroke, or had to care 
				for someone who has, you will know that these diseases can 
				seriously affect the life of both the patient and his or her 
				family. The effects can even reach beyond the family to the 
				community. Cardiovascular diseases are the world’s largest 
				killers, claiming 17.1 million lives a year. Risk factors for 
				heart disease and stroke include raised blood pressure, 
				cholesterol and glucose levels, smoking, inadequate intake of 
				fruit and vegetables, overweight, obesity and physical 
				inactivity. Cardiovascular disease is a major cause of 
				disability and premature death throughout the world, and 
				contributes substantially to the escalating costs of health 
				care.<br>
				<br>
				Of an estimated 58 million deaths globally from all causes in 
				2005, cardiovascular disease (CVD) accounted for 30 per cent. 
				This proportion is equal to that due to infectious diseases, 
				nutritional de&#64257;ciencies, and maternal and perinatal conditions 
				combined. It is important to recognise that a substantial 
				proportion of these deaths (46 per cent) were of people under 70 
				years of age, in the more productive period of life; in 
				addition, 79 per cent of the disease burden attributed to 
				cardiovascular disease is in this age group.<br>
				<br>
				Between 2006 and 2015, deaths due to noncommunicable diseases 
				(half of which will be due to cardiovascular disease) are 
				expected to increase by 17 per cent, while deaths from 
				infectious diseases, nutritional de&#64257;ciencies, and maternal and 
				perinatal conditions combined are projected to decline by 3 per 
				cent. Almost half the disease burden in low- and middle-income 
				countries is already due to noncommunicable diseases.<br>
				<br>
				A signi&#64257;cant proportion of this morbidity and mortality could be 
				prevented through population based strategies, and by making 
				cost-effective interventions accessible and affordable, both for 
				people with established disease and for those at high risk of 
				developing disease.<br>
				<br>
				To address the rising burden of noncommunicable diseases, in May 
				2000, the 53rd World Health Assembly adopted the WHO Global 
				Strategy for the Prevention and Control of Noncommunicable 
				Diseases. In doing so, it placed noncommunicable diseases on the 
				global public health agenda. <br>
				<br>
				Since then, WHO has strengthened its efforts to promote 
				population-wide primary prevention of noncommunicable diseases, 
				through the Framework Convention on Tobacco Control and the 
				Global Strategy for Diet, Physical Activity and Health. These 
				activities target common risk factors that are shared by CVD, 
				cancer, diabetes and chronic respiratory disease, and their 
				implementation is critical if the growing burden of 
				noncommunicable diseases is to be controlled. <br>
				<br>
				These measures should make it easier for healthy people to 
				remain healthy, and for those with established CVD or at high 
				cardiovascular risk to change their behaviour. However, 
				population wide public health approaches alone will not have an 
				immediate tangible impact on cardiovascular morbidity and 
				mortality, and will have only a modest absolute impact on the 
				disease burden. By themselves they cannot help the millions of 
				individuals at high risk of developing CVD (Table 1) or with an 
				established CVD. A combination of population-wide strategies and 
				strategies targeted at high risk individuals is needed to reduce 
				the cardiovascular disease burden. The extent to which one 
				strategy should be emphasized over the other depends on 
				achievable effectiveness, as well as cost-effectiveness and 
				availability of resources.<br>
				<br>
				<b><font size="2" color="#CC3333">CVD prevention and control: 
				missed opportunities</font></b><br>
				<br>
				Although many cardiovascular diseases (CVDs) can be treated or 
				prevented, an estimated 17.1 million people die of CVDs each 
				year. A substantial number of these deaths can be attributed to 
				tobacco smoking, which increases the risk of dying from coronary 
				heart disease and cerebrovascular disease 2-3 fold. The risk 
				increases with age and is greater for women than for men. In 
				contrast, cardiac events fall 50 per cent in people who stop 
				smoking and the risk of CVDs, including acute myocardial 
				infarction, stroke and peripheral vascular disease, also 
				decreases significantly over the first two years after stopping 
				smoking.<br>
				<br>
				Continuing to smoke after myocardial infarction or coronary 
				revascularisation can have serious clinical consequences. Even 
				eight years after myocardial infarction, the mortality of 
				post-myocardial infarction patients who continue to smoke is 
				double that of quitters. Further, those who do not stop smoking 
				after coronary revascularisation also have a two-fold higher 
				risk of re-infarction and death.<br>
				<br>
				Studies indicate that although doctors are knowledgeable about 
				the risks of CVDs associated with tobacco smoking, they are not 
				sufficiently prepared to help their patients stop smoking. Even 
				though physicians identify a substantial number of smokers 
				during consultations, for example, many patients do not receive 
				counseling to help them quit. Smoking cessation is the most 
				cost-effective intervention for patients with documented CVDs, 
				and efficacious programmes have been developed. The challenge is 
				to get these programmes more widely used, and doctors and nurses 
				should seize every opportunity to encourage patients to stop 
				smoking.<br>
				<br>
				The mission of the WHO Cardiovascular Diseases Programme is to 
				provide global leadership in the prevention and control of CVDs, 
				and to assist Member States reduce the toll of morbidity, 
				disability and premature mortality due to CVDs.<br>
				<br>
				<font color="#CC3333" size="2"><b>Key messages to protect heart 
				health</b></font><br>
				<br>
				Heart attacks and strokes are major – but preventable – killers 
				worldwide.<br>
				<br>
				Over 80 per cent of cardiovascular disease deaths take place in 
				low-and middle-income countries and occur almost equally in men 
				and women. Cardiovascular risk of women is particularly high 
				after menopause.<br>
				<br>
				Tobacco use, an unhealthy diet, and physical inactivity increase 
				the risk of heart attacks and strokes.<br>
				<br>
				Cessation of tobacco use reduces the chance of a heart attack or 
				stroke.<br>
				Engaging in physical activity for at least 30 minutes every day 
				of the week will help to prevent heart attacks and strokes.<br>
				<br>
				Eating at least five servings of fruit and vegetables a day, and 
				limiting your salt intake to less than one teaspoon a day, also 
				helps to prevent heart attacks and strokes.<br>
				High blood pressure has no symptoms, but can cause a sudden 
				stroke or heart attack. Have your blood pressure checked 
				regularly.<br>
				<br>
				Diabetes increases the risk of heart attacks and stroke. If you 
				have diabetes control your blood pressure and blood sugar to 
				minimise your risk.<br>
				<br>
				Being overweight increases the risk of heart attacks and 
				strokes. To maintain an ideal body weight, take regular physical 
				activity and eat a healthy diet.<br>
				<br>
				Heart attacks and strokes can strike suddenly and can be fatal 
				if assistance is not sought immediately<br>
				<br>
				<b>What are cardiovascular diseases?<br>
				</b><br>
				Cardiovascular diseases (CVDs) are a group of disorders of the 
				heart and blood vessels and include:<ul>
							<li>
							<p align="justify" class="style13">coronary heart 
							disease – disease of the blood vessels supplying the 
							heart muscle</li>
							<li>
							<p align="justify" class="style13">cerebrovascular 
							disease – disease of the blood vessels supplying the 
							brain</li>
							<li>
							<p align="justify" class="style13">peripheral 
							arterial disease – disease of blood vessels 
							supplying the arms and legs</li>
							<li>
							<p align="justify" class="style13">rheumatic heart 
							disease – damage to the heart muscle and heart 
							valves from rheumatic fever, caused by streptococcal 
							bacteria</li>
							<li>
							<p align="justify" class="style13">congenital heart 
							disease – malformations of heart structure existing 
							at birth</li>
						</ul>
						<p align="justify" class="style13">deep vein thrombosis 
						and pulmonary embolism – blood clots in the leg veins, 
						which can dislodge and move to the heart and lungs.<br>
						<br>
						Heart attacks and strokes are usually acute events and 
						are mainly caused by a blockage that prevents blood from 
						flowing to the heart or brain. The most common reason 
						for this is a build-up of fatty deposits on the inner 
						walls of the blood vessels that supply the heart or 
						brain.<br>
						<br>
						<b>Strokes can also be caused by bleeding from a blood 
						vessel in the brain or from blood clots.<br>
						What are the risk factors for cardiovascular disease?</b><br>
						<br>
						The most important behavioural risk factors of heart 
						disease and stroke are unhealthy diet, physical 
						inactivity and tobacco use. Behavioural risk factors are 
						responsible for about 80 per cent of coronary heart 
						disease and cerebrovascular disease.<br>
						<br>
						The effects of unhealthy diet and physical inactivity 
						may show up in individuals as raised blood pressure, 
						raised blood glucose, raised blood lipids, and 
						overweight and obesity; these are called ‘intermediate 
						risk factors’.<br>
						<br>
						There are also a number of underlying determinants of 
						CVDs, or, if you like, “the causes of the causes”. These 
						are a reflection of the major forces driving social, 
						economic and cultural change – globalisation, 
						urbanisation, and population ageing. Other determinants 
						of CVDs are poverty and stress.<br>
						<br>
						<b>What are common symptoms of cardiovascular diseases?</b><br>
						<br>
						Often, there are no symptoms of the underlying disease 
						of the blood vessels. A heart attack or stroke may be 
						the first warning of underlying disease. Symptoms of a 
						heart attack include:<br>
						pain or discomfort in the centre of the chest;<br>
						<br>
						pain or discomfort in the arms, the left shoulder, 
						elbows, jaw, or back.<br>
						<br>
						In addition the person may experience difficulty in 
						breathing or shortness of breath; feeling sick or 
						vomiting; feeling light-headed or faint; breaking into a 
						cold sweat; and becoming pale. Women are more likely to 
						have shortness of breath, nausea, vomiting, and back or 
						jaw pain.<br>
						<br>
						The most common symptom of a stroke is sudden weakness 
						of the face, arm, or leg, most often on one side of the 
						body. Other symptoms include sudden onset of: numbness 
						of the face, arm, or leg, especially on one side of the 
						body; confusion, difficulty speaking or understanding 
						speech; difficulty seeing with one or both eyes; 
						difficulty walking, dizziness, loss of balance or 
						coordination; severe headache with no known cause; and 
						fainting or unconsciousness.<br>
						<br>
						People experiencing these symptoms should seek medical 
						care immediately.<br>
						<br>
						<b>Why are cardiovascular diseases a development issue 
						in low- and middle-income countries?</b><br>
						<br>
						Over 80 per cent of the world’s deaths from CVDs occur 
						in low- and middle-income countries. People in low- and 
						middle-income countries are more exposed to risk factors 
						leading to CVDs and other noncommunicable diseases and 
						are less exposed to prevention efforts than people in 
						high-income countries.<br>
						<br>
						People in low- and middle-income countries who suffer 
						from CVDs and other noncommunicable diseases have less 
						access to effective and equitable health care services 
						which respond to their needs (including early detection 
						services).As a result, many people in low- and 
						middle-income countries die younger from CVDs and other 
						noncommunicable diseases, often in their most productive 
						years.The poorest people in low- and middle-income 
						countries are affected most. At household level, 
						sufficient evidence is emerging to prove that CVDs and 
						other noncommunicable diseases contribute to poverty. 
						For example, catastrophic health care expenditures for 
						households with a family member with CVD can <br>
						be 30 per cent or more of annual household spending.<br>
						<br>
						At macro-economic level, CVDs place a heavy burden on 
						the economies of low- and middle-income countries. Heart 
						disease, stroke and diabetes are estimated to reduce GDP 
						between 1 and 5 per cent in low- and middle-income 
						countries experiencing rapid economic growth, as many 
						people die prematurely. For example, it is estimated 
						that over the next 10 years (2006-2015), China will lose 
						$558bn in foregone national income due to the 
						combination of heart disease, stroke and diabetes.<br>
