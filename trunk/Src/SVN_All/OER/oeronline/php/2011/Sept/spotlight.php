						<b><font size="2" color="#CC3333">Heart-healthy 
						lifestyle<br>
						</font></b><br>
						Cardiovascular (heart) disease and stroke are the major 
						cause of death in this part of the world<br>
						<br>
						One reason is undeniably a lack of commitment to a 
						heart-healthy lifestyle. Your lifestyle is not only your 
						best defence against heart disease and stroke, it’s also 
						your responsibility.<br>
						<br>
						A heart-healthy lifestyle includes the ideas listed in 
						the heart below<br>
						By following these three simple steps (ABC) you can 
						reduce all of the modifiable risk factors for heart 
						disease, heart attack and stroke.<br>
						<br>
						A: Avoid Tobacco<br>
						B: Become more active<br>
						C: Choose good nutrition<br>
						<br>
						<b><font size="2" color="#CC3333">Quit Smoking</font></b><br>
						<br>
						If you smoke, quit. If someone in your household smokes, 
						encourage them to quit. We know it’s tough. But it’s 
						tougher to recover from a heart attack or stroke or to 
						live with chronic heart disease. Commit to quit. We’re 
						here to help if you need it. Remember passive smoking is 
						equally bad as active smoking.<br>
						<br>
						Reduce Cholesterol<br>
						Fat lodged in your arteries is a disaster waiting to 
						happen. Sooner or later it could trigger a heart attack 
						or stroke. You’ve got to reduce your intake of saturated 
						fat, trans fat and cholesterol and get moving. If diet 
						and physical activity alone don’t get those numbers 
						down, then medication may be the key. Take it just like 
						the doctor orders.<br>
						Choose good nutrition<br>
						A healthy diet is one of the best weapons you have to 
						fight cardiovascular disease. The food you eat (and the 
						amount) can affect other controllable risk factors: 
						cholesterol, blood pressure, diabetes and overweight. 
						Choose nutrient-rich foods – which have vitamins, 
						minerals, fiber and other nutrients but are lower in 
						calories – over nutrient-poor foods. A diet rich in 
						vegetables, fruits, whole-grain and high-fibre foods, 
						fish, lean protein and fat-free or low-fat dairy 
						products is the key. And to maintain a healthy weight, 
						coordinate your diet with your physical activity level 
						so you’re using up as many calories as you take in.<br>
						Control your Blood pressure<br>
						It’s the single largest risk factor for stroke. Stroke 
						is a major killer and one of the leading causes of 
						disability. Stroke recovery is difficult at best and you 
						could be disabled for life. Shake that salt habit, take 
						your medications as recommended by your doctor and get 
						moving. Those numbers need to get down and stay down. 
						Your goal is less than 120/80 mmHg.<br>
						Exercise<br>
						Be physically active every day. Research has shown that 
						getting at least 30 minutes of physical activity on 5 or 
						more days of the week can help lower blood pressure, 
						lower cholesterol and keep your weight at a healthy 
						level. But something IS better than nothing. If you’re 
						doing nothing now, start out slow. Even 10 minutes at a 
						time may offer some health benefits. Studies show that 
						people who have achieved even a moderate level of 
						fitness are much less likely to die early than those 
						with a low fitness level.<br>
						<br>
						<b><font size="2" color="#CC3333">Watch your weight</font></b><br>
						<br>
						Obesity is an epidemic, not only for adults but also for 
						children. An epidemic is when a health problem is out of 
						control and many people are affected by it. Fad diets 
						and supplements are not the answer. Good nutrition, 
						controlling calorie intake and physical activity are the 
