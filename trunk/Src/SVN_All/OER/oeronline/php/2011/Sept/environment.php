				<font face="Verdana" size="1" class="style11" style="font-size: 8pt">
				The project site of Saih Nihayada Gas Depletion Compression (SNDC) 
				of Al Hassan Engineering Co. was set up with a special emphasis 
				on environment protection and prevention of pollution. The 
				company has accorded great importance to maintain all equipment 
				in the plant to keep them in good working condition so that 
				emissions from the plant can be controlled.<br>
				<br>
				The company could also reduce its water wastage considerably by 
				properly conserving and managing the water. The entire water 
				distribution system is being done through pipes starting from RO 
				Plant to the end user, thereby avoiding transportation through 
				trucks. It helps to save fuel and reduce air pollution. The 
				waste water is recycled through Membrane Bio Reactor Type STP. 
				The water thus produced is of good quality and is being used for 
				irrigation, borrow pits, and dust control. Almost 100 per cent 
				of the water produced is used effectively without being 
				disposing in the evaporation pond.</font><font face="Verdana" size="1" class="style11" style="font-size: 11px"><br>
				<br>
				</font>
				<font face="Verdana" size="1" class="style11">
				<font color="#C00000" size="2"><b>Reducing pollution</b></font></font><font face="Verdana" size="1" class="style11" style="font-size: 11px"><br>
				<br>
				</font>
				<font face="Verdana" size="1" class="style11" style="font-size: 8pt">
				As the temporary facilities required 4 MVA (4,000 KVA) of power 
				during the construction phase of the project (two years), the 
				company has taken a unique initiative in getting temporary power 
				by tapping the power from nearby overhead line which is 3.2 km 
				away, thereby avoiding the usage of diesel gensets to produce 
				power and reducing pollution and carbon emissions. <br>
				<br>
				Other initiatives to reduce the carbon footprint include 
				reduction of the usage of plastic cups and plates and 
				encouraging the usage of glass or porcelain cups, and other 
				environmentally friendly cups and plates. The company also 
				encourages two side printing, electronic mail sending / 
				receiving which also contributed to avoid wastage of papers.<br>
				<br>
				In order to reduce the usage of bottled water, the company 
				supplies water produced from Reverse Osmosis Plant to the water 
				coolers through pipeline without storing in the plastic cans. 
				Tests are carried on the water samples collected from the 
				farthest point regularly to check the quality of water through 
				approved laboratory.<br>
				<br>
				The company has also used interlocking tiles instead of concrete 
				for walkways in camps. Though the initial cost of construction 
				is more, considering the repeated use of materials for other 
				sites, it works out to be economical. This in turn leads to 
				reduction in carbon emission and also elimination of disposal of 
				concrete debris.<br>
				<br>
				Besides, trees were planted in the staff camp as a part of 
				environment protection programme at SNDC site. The company 
				closely monitors the emissions released by the plant and will 
				take steps to control the emissions from time to time.<br>
