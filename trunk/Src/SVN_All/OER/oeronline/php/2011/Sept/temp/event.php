				<b><font size="2">
				<img border="0" src="images/event2-may2011.jpg" align="left" style="border: 0px solid #808080; ">PERFORMING WELL<br>
				</font></b><br>
				I thoroughly enjoyed reading your cover story on Oman’s best 
				banks. Overall, the banking sector in Oman has performed 
				reasonably well, despite a few banks showing a lackadaisical 
				performance. It was heartening to note that assets of commercial 
				banks and credit outstanding both showed a reasonable increase. 
				Looking forward, it seems likely that the banking sector will 
				show good growth rather than stupendous growth, as the economy 
				is also growing steadily. However, the non-performing assets of 
				the banking sector as a whole, except a few, is nowhere near 
				acceptable standards. Banks will have to ensure tighter 
				monitoring of their asset quality to ensure that there is no 
				further deterioration in the asset quality. There may be no 
				acceptable norm, but it is largely believed that 2-3 per cent 
				would be acceptable. Some of the banks in Oman have a long way 
				to go to achieve these levels. <br>
				<br>
				<i><font color="#0099FF">Nadalia Fernando</font> <br>
				CBD, Ruwi </i>
						<p align="justify" class="style13">
				<br>
				<b><font size="2">OIL RISE TO BENEFIT GDP OF GCC <br>
				</font></b><br>
				The GCC region will do well riding piggy-back on higher oil 
				prices. Most of the GCC countries including Oman have budgeted 
				oil price at significantly lower levels. In fact, Oman has based 
				its budget on crude oil prices at $55 per barrel. The present 
				crude oil prices will ensure that Oman is well placed as far as 
				its budget is concerned. However, emerging economies will face a 
				serious threat with rising crude stoking inflation. India and 
				China are particularly vulnerable, as they are already running 
				high inflation levels. Only time will tell whether inflation 
				will have an adverse effect on economic growth in these 
				countries.<br>
