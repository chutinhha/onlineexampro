				<img border="0" src="images/event1-may2011.jpg" width="200" height="302" align="left" style="border: 0px solid #808080; "><p align="justify" class="style13">
				Come May end, Oman will witness the most high profile event of 
				its kind targeted primarily at the top echelons of the Arabic 
				speaking business community in the Sultanate. Setting up new 
				benchmarks in the industry, for the first time, corporates in 
				Oman will be felicitated based on a highly qualitative 
				performance survey done in a transparent manner as per the best 
				international standards. <br>
				<br>
				To recognise the top achievers in Oman’s corporate world, Alam 
				al-Iktisaad Wal A’mal (AIWA) has instituted Awards for Oman’s 
				Best Performing Companies. Alam al-Iktisaad Wal A’mal (AIWA), 
				published by United Media Services (UMS), is Oman’s leading 
				Arabic monthly business magazine.<br>
				In a red carpet awards evening at Al Bustan Palace on May 30, 
				2011, AIWA will felicitate the top performing listed companies 
				in Oman during 2010. HE Sheikh Sa’ad bin Mohammed al Mardhoof al 
				Sa’adi, minister of commerce &amp; industry, will be the Guest of 
				Honour at the event. <br>
				<br>
				A networker’s dream, the event will be attended by the who’s who 
				of Muscat corporate high life. The premium event will attract 
				more than 250 top business leaders, CEOs and senior government 
				officials from a cross-section of industries, ministries and 
				government bodies. <br>
				The event will be organised under the patronage of Muscat 
				Securities Market (MSM). Gulf Baader Capital Markets SAOC (GBCM) 
				is the knowledge partner whereas KPMG is the audit partner for 
				the ranking process used for the awards. “Achieving the pinnacle 
				of corporate performance is the dream of every CEO. But very few 
				CEOs manage to convert their dream into a reality. Alam al-Iktisaad 
				Wal A’mal Awards for Oman’s Best Performing Companies endeavours 
				to recognise and celebrate the excellent performance of the 
				listed companies <br>
				who have made it to the top,” says Sandeep Sehgal, CE, UMS.<br>
				<br>
				Overall 15 Top Ranking Companies will receive the coveted 
				trophy. In addition, AIWA will also felicitate two prominent 
				Omani nationals who have done the nation proud by excelling in 
				the fields of business and economy. The AIWA Awards to the 
				selected personalities will be made in two categories – AIWA 
				Global Omani of the Year Award and AIWA Life Time Achievement 
				Award. <br>
				<br>
