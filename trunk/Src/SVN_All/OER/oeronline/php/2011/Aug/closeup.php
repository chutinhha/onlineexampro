						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Kuwait’s metro ambitions<br>
&nbsp;</font></b><p align="justify" class="style13">
				<span class="style14"><strong>Kuwait’s planned metro project 
				should strengthen Kuwait’s positioning as a key business hub in 
				the region and entice foreign direct investments to the country<br>
				</strong></span><br>
				Kuwait’s planned metro project could help the country strengthen 
				its regional competitive position. Once completed, this project 
				is expected to add a new dimension to Kuwait’s infrastructure 
				and the competitiveness of its economy.<br>
				<br>
				Kuwait can afford the planned metro project, as evidenced by 
				repetitive budgetary surpluses. The total direct and indirect 
				cost of the planned project is estimated at $10bn (Dh36.7bn). 
				The country posted a surplus of $30bn in fiscal year 2010-2011, 
				which ended in March. An ambitious plan calls for the 
				implementation of the project in the first quarter of 2013. As 
				such, the tendering process is due to take place in 2012 after 
				the consultants prepare the necessary materials. One such sign 
				of the seriousness relates to the selection of two international 
				firms to serve as consultants for the scheme.<br>
				<br>
				<font color="#000080" size="2"><b>Major challenges</b></font><br>
				<br>
				Kuwait lags behind all other Gulf Cooperation Council (GCC) 
				countries except for Bahrain at the Global Competitiveness 
				Report 2010-2011. Published by the World Economic Forum, the 
				report’s index, known as Global Competitiveness Index or GCI, is 
				noted for taking a holistic approach vis-à-vis the performance 
				of the economies it reviews.<br>
				<br>
				The index ranks economies on the basis of their achievements in 
				three broad categories: basic requirements, efficiency enhancers 
				and innovation and sophistication factors. The basic 
				requirements category is divided into institutions, 
				infrastructure, macroeconomic stability, health and primary 
				education.<br>
				<br>
				The efficiency enhancers category comprises higher education and 
				training, goods and market efficiency, labour market efficiency, 
				financial market sophistication, technological readiness and 
				market size. The innovation and sophistication factors category 
				is made up of business sophistication and innovation. The three 
				categories of basic requirements, efficiency enhancers and 
				innovation and sophistication are brought together in the 
				formation of GNI.<br>
				Kuwait is ranked last amongst the GCC countries in 2010-2011 
				Global Information Technology Report, specifically in its 
				Network Readiness Index (NRI). The report ranks countries on the 
				basis of their appreciation of information and communication 
				technologies (ICT).<br>
				<br>
				The annual report is a joint effort by the World Economic Forum 
				and INSEAD, a renowned international graduate business school 
				and research institution, with campuses in France, Singapore and 
				the UAE.<br>
				<br>
				In order to arrive at NRI, the comprehensive study relies on 68 
				variables which are classified into three broad categories: (1) 
				market, political, regulatory and infrastructure environment (2) 
				individual, business and government readiness for ICT and (3) 
				individual, business and government use of ICT services. <br>
				<br>
				Countries are ranked according to their performance on NRI. The 
				UAE leads the GCC and Arab World positioning itself at number 24 
				amongst 138 countries ranked in the latest report. Qatar, 
				Bahrain, Saudi Arabia and Oman stood at number 25, 30, 33, and 
				41, respectively. But Kuwait was ranked 75 globally in the 
				report.<br>
				<br>
				Kuwait trails fellow Gulf Cooperation Council (GCC) countries 
				when it comes to foreign direct investment (FDI), as indicated 
				by the World Investment Report 2010, issued by the World 
				Conference on Trade and Development (Unctad).<br>
				<br>
				Total inward FDI in Kuwait was only $145mn in 2009, the last 
				year for which comprehensive figures are available. Conversely, 
				Saudi Arabia attracted a hefty $35.5bn in the same year, the 
				highest for any country in the West Asia region. Logistical 
				complexity is partly blamed for Kuwait’s inability to entice 
				foreign investments. Hence, the Kuwait Metro project should help 
				strengthen the country’s business prospects. <br>
				<br>
				<font color="#000080" size="2"><b>Legal hassles </b></font><br>
				<br>
				One can only hope the country’s extraordinary legislative branch 
				would not derail the project on any ground. The country’s 
				parliament has a history of putting the brakes on ambitious 
				projects. Also, successive parliaments failed to endorse Project 
				Kuwait, in turn aiming at generating an additional 450,000 
				barrels per day from four fields located in the north and west 
				of the country. The development requires an investment of $8bn 
				over a 20-year period.<br>
				International oil firms have the means including the technology 
				and know-how to carry out such an ambitious scheme. However, on 
				several occasions, ultra conservative MPs have managed to block 
				the project citing a clause within the Kuwaiti constitution that 
				bars foreign ownership of the country’s hydrocarbon resources.<br>
				<br>
				However, on several occasions, ultra conservative MPs have 
				managed to block the project citing a clause within the Kuwaiti 
				constitution that bars foreign ownership of the country’s 
				hydrocarbon resources. In addition, in 2008 the parliament 
				forced the authorities to cancel a 417.4bn project with Dow 
				Chemical of the US to create the world’s largest maker of 
				polyethylene. The move reflected assertions, of some 
				legislators, of the presence of kickbacks in the project, hence 
				the relatively high cost. Eventually, the two sides had to avert 
				to a legal system to settle the dispute, requiring Kuwait to 
				offer a large amount of money to end the case.<br>
				<br>
				Undoubtedly, the ambitious project should strengthen Kuwait’s 
				positioning as a key business hub in the region, and thereby 
				entice foreign direct investments.<br>
				<br>
				Kuwait has notable attractive qualities including a relatively 
				high per capita income of $46,000, the third highest of its kind 
				in GCC after Qatar and the UAE. In reality, Kuwaiti nationals 
				are noted for their willingness to spend and invest their 
				resources.<br>
				<br>
				<font color="#000080" size="2"><b>Investing at home</b></font><br>
				<br>
				At the same time, development of a local rail network could 
				press Kuwaitis to invest at home. Of all GCC investors, Kuwaiti 
				nationals are noted for their international outlook. Certainly, 
				this is a notable quality but investment at home cannot be 
				overlooked. <br>
				<br>
				Among others, the metro should ease commuting for expatriates, 
				who form most of the country’s work force. Kuwait is notorious 
				for swirling sand during the winter, which makes commuting by 
				road risky. All told, the Kuwait Metro should streamline 
				business and working conditions in the country, thereby helping 
				different stakeholders including investors and workers.<br>
