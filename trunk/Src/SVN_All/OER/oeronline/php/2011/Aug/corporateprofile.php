						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>A veritable mosaic</strong></span><p class="style15">
				<span class="style14"><strong>Business Gateways International 
				offers an innovative international framework that provides SMEs, 
				large corporate houses and ministries with a robust platform to 
				showcase their business to the world. <i><font color="#808080">
				Muhammed Nafie</font></i> reports</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/corp1-aug.jpg" width="450" height="419" style="border: 1px solid #808080; " hspace="40"></font><p class="style15">
				<br>
				Though cross-border networking of businesses was made easy by 
				globalisation which triggered free flow of ideas, people, goods, 
				services and capital across the world, small and medium scale 
				industries (SMEs) were not able to utilise this bonanza 
				successfully. They tended to stay put, hardly foraying into the 
				areas and opportunities beyond their precincts. Apparently, they 
				were not able to do so, without an effective mechanism to 
				showcase their strength and explore new avenues of expansion for 
				them. A common platform for connecting businesses and promoting 
				partnership both at national and international level can bring 
				about a lot of changes in this area. It was from the quest for 
				such a framework that the idea of Business Gateways 
				International (BGI) was born.<br>
				<br>
				The concept of Business Gateways evolved after several 
				brainstorming, analysis and study sessions which lasted for 
				almost two years. Says Hemant Murkoth, CEO of BGI, “When we 
				tried to understand and look at various markets and territories 
				in the country, we noticed a lot of gaps with regards to 
				addressing some of the core business services that are needed in 
				the Sultanate. The concept which had a very humble beginning has 
				now expanded to a global framework.”<br>
				<br>
				Identifying a massive gap in connecting a number of credible 
				companies within the Sultanate, Business Gateways International, 
				headquartered at Knowledge Oasis Muscat (KOM), is in the process 
				of developing an Omani National Business Framework connecting 
				all organisations and service providers in Oman on a common 
				business platform. “We treat ourselves as national ambassadors 
				as we seek to showcase the economic strength of Oman 
				internationally,” says Murkoth. Explaining the national 
				framework of the company, he adds, “We have a specific 
				membership system; the service is targeted at the entire 
				spectrum of the national business community with special 
				emphasis on SMEs. At the national level, we would help the 
				companies to be visible, discover existing business potential 
				and partner with likeminded organisations within the country. We 
				have already established our regional operations in Salalah”.<br>
				<br>
				Business Gateways International is also working very closely 
				with various investment and trade development authorities in the 
				country to brand Oman. “We want to integrate with all these 
				bodies, because at the end of the day, it is not just our 
				effort, but rather a collective effort to showcase Oman to the 
				world” explains Murkoth.<br>
				<br>
				<b><font size="2" color="#000080">Enhance, Expand, Excel</font></b><br>
				<br>
				The advantages an organisation can expect from BGI can be summed 
				up in its catchword: Enhance, Expand, Excel. ‘Enhance’ addresses 
				an organisation’s main challenge of visibility to the national 
				and international business community. “We try to showcase them 
				or make them more visible at the national and international 
				levels so they can attract better partners, products and best 
				practices from around the world. This will enhance their 
				existing business offerings and enhance customer efficiency,” 
				says Hemant. ‘Expand’ relates to the expansion and development 
				of a company both nationally and internationally. It helps a 
				company based in Muscat expand to Salalah or Nizwa or vice 
				versa. Besides, it assists the companies to explore the 
				opportunities to expand internationally and find foreign 
				partners. ‘Excel’ means the continuous support services BGI 
				offers to its members for business stimulation which includes 
				free tender feeds, projects information, events, forums, supply 
				chain network etc. which help them govern their business better 
				in the country.<br>
				<br>
				<b><font size="2" color="#000080">Business stimulation centres</font></b><br>
				<br>
				A purely Omani concept, Business Gateways has gone global by 
				setting up its stimulation centres across the world. “The idea 
				is to set up business stimulation centres all over the world. 
				Once we set up these particular gateways, it is like creating 
				one seamless layer which will integrate all international 
				companies on a common platform. That is the core identity of 
				Business Gateways. We encourage companies to become members 
				through our national systems,” avers Murkoth.<br>
				<br>
				Increasing exports and bringing in investment are the two 
				priority areas of all countries. BGI exactly seeks to do the 
				same. “We concentrate a lot on SMEs because we feel that SMEs 
				need more thrust and push. Governments are doing it in their own 
				way while we have created our own business model globally in the 
				countries where we are placed. Therefore, Business Gateways can 
				fit in perfectly, be it Oman, or any other country,” he adds. 
				Outside Oman, BGI has already opened its stimulation centre in 
				Tanzania aiming to tap into the entire East African market. 
				Tanzania will be the hub of Business Gateways’ East African 
				operations. BGI is also eagerly looking forward to foraying into 
				other GCC countries and Asia pacific regions. It is ambitiously 
				planning to set up offices in the GCC, Asia Pacific and European 
				regions. <br>
				<br>
				Besides, whenever it goes to new countries it chooses to 
				piggyback on some of the existing trade frameworks in those 
				countries and work closely with various government bodies. Says 
				Said Hilal al Ismaily, director, BGI, “In order to deliver the 
				relevant services we want a lot of government support. We work 
				closely with the trade bodies in those countries. In Tanzania, 
				we have already signed a Memorandum of Understanding (MoU) with 
				the Tanzanian Trade Development Authority (TANTRADE), government 
				of Tanzania. Together with TANTRADE we will work for attracting 
				investors from Oman to tap into the huge potential in Tanzania. 
				Omani companies coming to Tanzania needs a lot of government 
				support. With the MoU we can ensure that all the supports and 
				services can be provided.<br>
				<br>
				<b><font size="2" color="#000080">Web Portal</font></b><br>
				<br>
				The core delivery system of the gateway services is a powerful 
				web portal www.businessgateways.com that would help member 
				organisations to exhaustively showcase their business profile 
				information, tap into potential national business opportunities 
				and internationally expand their reach into target countries.<br>
				<br>
				“A company, who is looking for a potential partner, has to 
				update its website and make its portal known to all. Our portal 
				is providing a uniform structured visibility system; each 
				company is given pages to profile themselves which also includes 
				videos and multimedia presentations. Each company whether it is 
				from Oman or Singapore are presented in the same way,” explains 
				Murkoth.<br>
				<br>
				Coupled with this portal is a host of business advisory services 
				that are delivered from the gateway offices across the globe 
				which offers to all organisations virtual offices in the country 
				of their interest.<br>
				<br>
				Business Gateways is very upbeat on this unique concept and is 
				confident that it can contribute a lot to Oman’s business world 
				and slowly but steadily grow into an international framework 
				connecting countries on a single <br>
				business platform.</td>
