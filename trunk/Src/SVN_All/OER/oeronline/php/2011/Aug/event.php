						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>STAGE set for Oman Debate 
						2011</strong></span><p class="style15">
				<strong><span class="style14">The highly successful and much 
				anticipated annual business forum brought to you by OER will be 
				held in October<br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event1-aug.jpg" width="450" height="203" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>Over the past two years, the annual OER Debate has 
						evolved into an integrated business symposium that 
						encompasses key themes from the business environment of 
						today and tomorrow in the Sultanate of Oman.<br>
						<br>
						The last two editions of OER Debate had witnessed packed 
						audiences totalling over 400 movers and shakers from 
						Oman’s corporate world, all innovators in their 
						profession converging to witness the intense indepth 
						discussions on the state of the economy.<br>
						<br>
						Featuring key panelists from various business sectors 
						cutting across the industry, the OER Debate had 
						rivetting views and incisive questions about how Oman’s 
						corporate world should shoulder the responsibility of 
						complementing the government’s efforts to take the 
						country forward on its growth path.<br>
						<br>
						To give this annual forum a truly national platform, the 
						third edition of OER Debate has now been re-christened 
						as ‘Oman Debate 2011’. This transformation was but 
						inevitable due to the feedback received from audiences 
						of the earlier two editions who had wanted the debate to 
						have a wider and broader ambit.<br>
						<br>
						Fundamentally, Oman Debate 2011, brought to you by Oman 
						Economic Review (OER) in association with the Capital 
						Market Authority (CMA) will continue to maintain its 
						status as an unique platform where influential 
						individuals will share stories of best practices, 
						effective business case-studies and, most importantly, 
						attributes of true leadership. <br>
						<br>
						The event will cover globalisation and economics, human 
						capital, motivation and diplomacy with a strong focus on 
						the future of Oman through the idea of a new age of 
						leadership in a changing business landscape and economic 
						trends.<br>
						<br>
						For Oman Debate 2011, the hot topic of discussion is how 
						to cope up with the developments of the Arab Spring, 
						which has had its repercussions on the Sultanate of 
						Oman. The pro-reforms movement has forced a rethink 
						about government priorities and policies. This has taken 
						the leadership of this country back to the drawing board 
						to re-chart its future course of action. However, the 
						measures taken to address various issues have led to 
						various questions/concerns in its trail. OER Debate 
						raises a few of these: 
						<ol style="font-family: Tahoma; font-size: 10px">
							<li>
							<p align="justify" class="style13"><b>Job creation</b><span style="font-weight: 400"><br>
							Is creating 50,000 jobs in one year the solution to 
							the unemployment problem or are there structural 
							issues like changes in the education system, making 
							people employable, training etc that need to be 
							looked at.</li>
							<li>
							<p align="justify" class="style13"><b>Raising 
							salaries, sops and benefits: Can the corporate 
							sector foot the bill?</b><br>
							Is the private sector being made to foot an 
							unsustainable bill or has the corporate sector 
							failed to do its part in the development process? 
							</li>
							<li>
							<p align="justify" class="style13"><b>Omanisation: 
							Are these realisable goals or unrealistic 
							expectations?</b><br>
							The government has set Omanisation targets for 
							various sectors but the private sector is struggling 
							to find suitable/employable Omanis to fill those 
							positions. </li>
							<li>
							<p align="justify" class="style13"><b>Oman: At the 
							crossroads</b><br>
							Is this a watershed moment in Oman’s socio-economic 
							history where everything that was held as sacred 
							needs to be questioned and redefined?</span> <br>
&nbsp;</li>
						</ol>
