						How to be more than just better</font></b><p align="justify" class="style3">
				<span class="style5"><strong>Rob Yeung’s ‘The Extra One Per 
				Cent: How Small Changes make Exceptional People’ chronicles the 
				skills and behaviours that distinguish high achievers from their 
				less successful counterparts. An OER REVIEW<br>
				<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/browsingcorner1-aug.jpg" style="border-style:solid; border-color:#808080; float: right" class="style4" width="200" height="287"></font>William 
				Chalk and Jasper Ahliquist are key account managers working for 
				a European information technology company. Having obtained 
				degrees from good universities and enjoying track records of 
				career progression, both men have passed a grueling selection 
				process that involved multiple rounds of interviews with senior 
				managers from across the organisation.<br>
				<br>
				But last year, one of these men was in the top 10 per cent of 
				the sale force, by virtue of selling more hardware, software and 
				services than 90 per cent of the other key account managers, 
				while the other one was in the bottom 10 per cent of the sales 
				team, being outsold by 9 in 10 of his colleagues. One of them 
				earned $482,520 in salary and bonuses, putting him in the top 10 
				per cent of the company while the other one earned $107,950, 
				putting him in the bootom 10 per cent.<br>
				<br>
				Why did they score at opposite ends of the scale, with one 
				emerging more vibrantly successful than the other? Why are some 
				people so much more successful than others? It was to unravel 
				this subtle yet crucial difference that distinguishes 
				exceptional people from everyone else, that Rob Yeung has 
				interviewed hundreds of high achievers in business, sports and 
				entertainment. By gaining insight into the minds of these 
				exceptionally successful people, and combining this information 
				with a wealth of scientific research, Rob could easily find out 
				the extra one per cent that helps them rise above the crowd and 
				be extraordinary. Thus was born Rob’s The Extra One Per Cent: 
				How Small Changes Make Exceptional People, which was hailed as 
				nothing short of a guide to high achievers. By combining 
				scientific research into the secrets of success, the 
				psychologist in Rob tackles this much talked about subject - the 
				formula of success - from a research-oriented perspective. The 
				book treads a nice line between being enjoyable and being 
				informative.<br>
				<br>
				Imagination, cherishing, authenticity, centeredness, 
				connectedness, daring, integration and vision are the eight 
				characteristics that were discussed in detail, each in a 
				dedicated chapter. Those who are gifted with this extra one per 
				cent may necessarily not be more intelligent; nor do they come 
				from either better or worse background or benefit from 
				extraordinary educations. One of the biggest differences, he has 
				seen between high achievers and their less successful 
				counterparts is that the exceptional ones are out there doing 
				things. “They take actions. They take notes. They make plans. 
				The initiate conversations and meetings and take steps every day 
				to edge them closer towards their goals and visions. They 
				consciously commit to self-improvement and make it a priority. 
				Rather than just talking about what they could or might do, they 
				get on with it,” writes Rob in the concluding chapter.<br>
				<br>
				In chronicling their journeys, Rob must have told only fragments 
				of their stories; but he has marvellously illustrated the 
				relevant capabilities – the skills and behaviours – that 
				everyone who wants to be successful in life and profession will 
				be able to learn and emulate.<br>
