						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Qualitative growth<br>
						</font></b><p align="justify" class="style13">
				<strong><span class="style14">Global Chemicals and Maintenance 
				Systems, part of Al Barami Group of Companies, was recently 
				awarded the ISO certification, thanks to its enviable reputation 
				in the market. An OER report<br>
				</span></strong><br>
				Global Chemicals and Maintenance Systems has been associated 
				with the power, water, transmission and distribution sector in 
				the Sultanate for over four decades and has established a 
				reputation in the market as a quality conscious company. 
				Global’s core expertise is in engineering, procurement and 
				construction (EPC) along with operation and maintenance in all 
				the three sectors.<br>
				<br>
				Apart from this, Global also supplies electro-mechanical 
				plants/equipments and chemicals to hospitals and companies 
				besides providing technical and managerial training.<br>
				<br>
				<b>
				<img border="0" src="images/cor-profilo1-april2011.jpg" width="150" height="173" align="left" style="border: 2px solid #808080">Catering to demand</b><br>
				<br>
				Established in 1963, Global today possesses more than 1200 
				qualified and experienced technicians, managerial staff and 
				other related professionals to meet the demands of government 
				and private sector for cost effective projects and operations of 
				electro-mechanical plants and utilities.<br>
				<br>
				Global is part of the Al Barami Group of Companies (BGC) - one 
				of the most enterprising and diversified business establishments 
				in the Sultanate prospering under the able leadership of Salem 
				Bin Ahmed Bin Khalfan Al Barami, Chairman and CEO.<br>
				<br>
				Talking about the history of the parent company, Abdul Hafidh Al 
				Barami, managing director, says that the history of Al Barami 
				Group of Companies goes back to 1973 when it was started as an 
				industrial trading company by four Omani partners and was 
				involved mainly in transportation, trading and agency handling.<br>
				<br>
				Later as the group expanded, Al Barami established a number of 
				companies. These include the Barami Steel Industry, 
				manufacturing BRC engineering mesh; Al Barami Building Materials 
				for steel, cement, tiles, electric ware, road marking and road 
				accessories; and an investment vehicle known as Barami 
				Investment Company.<br>
				<br>
				Global, which was initially a JV between foreign investors, 
				became part of the company in 1976.<br>
				<br>
				<b>
				<img border="0" src="images/cor-profilo2-april2011.jpg" width="150" height="193" style="border: 2px solid #808080" align="left">Providing customer satisfaction</b><br>
				<br>
				Says Abdul Hafidh, “Our mission is to maintain our brand 
				leadership by providing state-of-the-art services and 
				continuously improving all our business processes to meet the 
				highest level of satisfaction for our customers, at the same 
				time remaining committed to our employees through HR practices, 
				which is also serving the community.”<br>
				<br>
				Continuing further he says, “Our vision is to be regarded as a 
				leader in every sphere of our activity and become the first 
				choice and trusted name in the marketplace.”<br>
				<br>
				“Even though ours is a family run-company, we believe in 
				corporate governance and we want our company to be passed on to 
				our next generation as well. Therefore, last year we had a 
				consultant to review our HR policies and corporate governance 
				structure,” says Abdul Hafidh, who is one of the four brothers 
				who are involved with the company.<br>
				<br>
				Talking about HR policies, Al Barami has realised that there is 
				intense competition in the market and has constantly been 
				striving to provide a friendly corporate working environment to 
				retain the best employees, by motivating them and showing them a 
				clear career path.<br>
				<br>
				The best example of such employee-friendly policies was a recent 
				decision taken by the management to have a five-day working 
				week. “The demand was raised by the employees and we looked into 
				it and decided to implement it. We know that the most valuable 
				asset for any company is its human capital and we need to 
				motivate them and raise their productivity,” says Abdul Hafidh.<br>
				<br>
				Each of the group companies has their own general managers who 
				enjoy the full faith of the management and have been given 
				independent responsibilities to grow their business units. This 
				has resulted in the group companies becoming successful in their 
				own right.<br>
				<br>
				<b>ISO certification</b><br>
				<br>
				Global was recently awarded the ISO certification thanks to the 
				organisation’s endeavour to implement the latest technologies 
				for project monitoring and computerised maintenance, which 
				enabled it to achieve the highest level of quality, availability 
				and reliability of the equipment and utilities.<br>
				<br>
				“The ISO certification is definitely a big achievement for us 
				and I am proud that I have a great team that made it possible. 
				The certification will be an advantage to set ourselves apart in 
				the market and have a competitive edge,” says Abdul Hafidh.<br>
				<br>
				Concurring with him is Tabussam Hayat, general manager, 
				technical affairs, who says, “Our core business is EPC and O&amp;M 
				of power plants (specialising in diesel power generation), water 
				and desalination plants as well as in T&amp;D. The ISO certificate 
				will help us to enter into previously unchartered territory and 
				bid for big tenders from companies like PDO.”<br>
				<br>
				“We were able to qualify for ISO certificate in a very short 
				period of three months thanks to a dedicated team of people led 
				by Mohsin Ali Zain, quality management representative, who did a 
				wonderful job. Thanks to the ISO certification, we were able to 
				bid for a tender floated by Oxy,” Hayat discloses.<br>
				<br>
				Its leadership position in diesel power is unmatched as seen by 
				the fact that Oman’s one of the biggest diesel generated power 
				plant is actually being constructed by Global and it is coming 
				up in Duqm, the most happening industrial area after Sohar.<br>
				<br>
				“We are doing the EPC for this power project and it is in the 
				last stages of completion. Actually it is in the commissioning 
				stages,” informs Hayat.<br>
				<br>
				“The company has a team of professionals with several years of 
				in-depth experience in the field, who strongly believe in 
				providing top-quality services and optimal operating cost to 
				clients,” he says.<br>
				<br>
				The company executes EPC projects from conceptual study, 
				engineering, procurements, constructions, commissioning and 
				start-ups. The projects division has a profound record of 
				accomplishment of more than 25 successful projects. The O&amp;M 
				divisions are ably supported by a professional and competent 
				support services department to get not only the best 
				procurements but also to carry out periodic supplier evaluation 
				for quality assurance.<br>
				<br>
				“Through our team experience and innovative approach, we deliver 
				the best within stipulated timelines with all statutory leading 
				requirements of health, safety and environment. We firmly 
				believe that client satisfaction is the hallmark of our 
				success,” points out Hayat.<br>
				<br>
				Meanwhile, Abdul Hafidh says that since the market is changing 
				and competition is increasing, the company is now increasing its 
				resources and equipping itself with new technologies and 
				skillsets. The ISO is a plus point to give the company an 
				entrance to bid for bigger projects. The company is also 
				venturing into the gas turbine power generating projects.<br>
				As for the future, the Al Barami Group is planning to go public 
				with an IPO. With such strong fundamental values, sound 
				corporate governance and an unbeatable reputation, the group has 
				certainly a long way to go.&nbsp;</td>
