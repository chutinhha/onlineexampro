						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						OPEC, Brent Crude and oil shares</font></b><p class="style15">
				<strong><span class="style14">North Sea Brent crude is expected 
				to fall as Saudi Arabia boosts output and global industrial 
				production softens<br>
				<br>
				<br>
				</span></strong>OPEC has faced numerous crises since its 
				establishment in 1960 to extract greater royalties from the 
				Seven Sister corporate colossi who once dominated the 
				international oil market. The closure of the Suez Canal, the 
				1973 October war in the Golan and the Suez, the 1974 Arab 
				embargo, the 1975 terrorist attack by Carlos the Jackal on the 
				OPEC Vienna secretariat, the fall of the Shah of Iran in 1979, 
				the 1980-88 Iran-Iraq war, the 1990 invasion of Kuwait, the 
				Lockerbie tragedy and UN sanctions against Libya, the 1999 oil 
				price collapse, the 2008 oil death spiral and the 2011 Arab 
				uprisings. OPEC’s greatest achievement in the past half century 
				has been sheer survival, a testament to the vision of its two 
				founders, the late Saudi Sheikh Abdullah Al Tariki and the 
				Venezuelan Juan Pablo Perez Alfonso.<br>
				<br>
				<font color="#000080" size="2"><b>OPEC’s swing producers</b></font><br>
				<br>
				Saudi Arabia plays a unique role in OPEC because it is the 
				largest producer, and boasts the largest, lowest cost reserves. 
				The kingdom has the ability to increase or decrease its output 
				because it has 3MBD spare capacity. So Saudi Arabia acts as the 
				swing producer and price discipline enforcer in OPEC. This is 
				exactly what Sheikh Yamani did in 1986, when oil prices plunged 
				down to seven dollars when the Saudis wanted to punish endemic 
				quota cheating by Nigeria and Iran. Saudi Arabia, Kuwait and the 
				UAE have acted as a united bloc in OPEC since the 1970s, since 
				their strategic interest is to stabilise oil prices and prevent 
				ruinous price shocks that could mean recession in the world 
				economy. Acting together, Riyadh, Kuwait and Abu Dhabi have 
				acted as the swing producers of OPEC, the de facto central banks 
				of black gold.<br>
				<br>
				In June 2011, the three GCC oil producers will act in unison 
				with Saudi oil minister Ali Al Naimi’s words, “deliver what is 
				needed to the oil market”. This means another 1.5 MBD to 
				compensate for loss of Libyan exports and the end of the Asian 
				refinery maintenance season and winter heating oil demand. <br>
				<br>
				In essence, no 2008 style price spiral and boom-bust cycle is 
				expected. This was the reason the IEA, led by the US Strategic 
				Reserve Fund, sold 60 MBD to preempt a speculative spiral, such 
				as the one that took prices to $148 in July 2008.<br>
				<br>
				Iran, Venezuela, Nigeria, Algeria and Libya know that this 
				scenario means lower prices and lower revenues since they do not 
				have the spare capacity to boost production in any case. There 
				is a supply shock in Dated Brent, the reason for its $20 spread 
				against West Texas and the Brent/Dubai spread is at a record 
				eight dollars. The conclusion is unmistakable, major fall in 
				North Sea Brent crude is imminent. I believe Brent crude will 
				fall at least $20 as the Saudis boost output, global industrial 
				production softens and NATO takes out Colonel Gaddafi. The 
				dismal June US job report only increases the soft patch/double 
				dip recession risk, a negative for oil prices. Saudi Arabia’s 
				oil production in 2011 will be the highest since 1979, the year 
				the Shah lost his Peacock Throne, and the Soviet Union invaded 
				Afghanistan. This time the wolf is here.<br>
				<br>
				A price war would be ominous for the future of OPEC. OPEC quotas 
				are irrelevant to the world’s two crude oil superpowers – Saudi 
				Arabia and Russia. Saudi Arabia and Iran have divergent economic 
				and political interests in the oil market and across the Arab 
				world. Under UN sanctions, without spare capacity, Iran cannot 
				retaliate against Saudi interests in OPEC. The price of Brent, 
				the world’s preeminent light sweet crude benchmark is destined 
				to fall in the next three months.<br>
				<br>
				<font color="#000080" size="2"><b>Pressure on prices</b></font>
				<br>
				<br>
				The global economic scenario suggests pressure on oil prices in 
				the next three months. The ECB and the People’s Bank of China 
				have both raised interest rates and the Eurozone debt crisis 
				could easily spread to Spain/Portugal while the UK Coalition 
				government enacts the most draconian government spending cuts 
				since Thatcher’s era in the 1980s. With 14 million jobless 
				workers in the US, the prospects for US gasoline demand growth 
				in the summer is not at all positive. The global economic soft 
				patch is all too real. This means a price war and lower oil 
				prices.The West Texas – Brent spread widened dramatically due to 
				the loss of Libyan light sweet crude (priced off Brent), the 
				North Sea maintenance season and the oil cargo glut in Cushing, 
				Oklahoma delivery point for NYMEX West Texas futures contract. 
				The Brent – WTI spread is a buy at 15 for a $25 target.<br>
				<br>
				The world’s leading oil companies have now discounted a $80-85 
				price range for Brent crude, well below current traded levels. 
				While I expect oil shares will fall in July and August as Brent 
				drifts lower, this is an opportunity to target major oil 
				companies that offer high production growth, long life reserves 
				and modest valuation metrics. Apache Petroleum (APA), whose 
				shares were pressured by its exploration and production acreage 
				in Egypt’s Western desert, trades at only nine times forward 
				earnings even though it offers 16 per cent ROE, 15 per cent 
				annual production growth and long life reserves in 
				Texas/Alberta, Argentina and the North Sea. Apache is a 
				potential takeover candidate for a Big Oil colossus interested 
				in replenishing reserves in a world with declining production 
				rates. This means Apache offers compelling value at $110 for a 
				$180-200 target.<br>
