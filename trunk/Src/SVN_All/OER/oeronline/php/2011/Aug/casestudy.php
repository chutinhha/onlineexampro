						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Towards nationalisation</strong></span><p class="style15">
				<strong><span class="style14">Successful nationalisation 
				requires a significant commitment from managements to make it 
				happen, according to a case study by Celerant<br>
&nbsp;</span></strong><p align="center" class="style13">
				<font face="Verdana" size="1">
				<a target="_blank" href="images/casestudy1big-aug.jpg">
				<img border="0" src="images/casestudy1sm-aug.jpg" width="450" height="318" style="border: 1px solid #808080; "></a></font><p align="justify" class="style13">
				<br>
				For all companies looking to build successful joint ventures 
				with developing countries, particularly in the Middle East and 
				Africa, a smooth transition to a nationally manned, sustainable 
				operation is vital if good relations are to be maintained. The 
				dilemma for private companies is that although sustainability is 
				the objective, the challenges vary from region to region and 
				country-to-country. This has never been truer than in Oman 
				today. Well-educated, hard working, skilled nationals are a rare 
				commodity in high demand. Simple economics determine that there 
				is a premium to pay for this scarce resource especially in view 
				of the recent pay hikes and a local workforce who wants to work 
				increasingly in public sector government bodies. The private 
				sector is set nationalisation targets in an environment which 
				does not compete on level terms. This is a particular challenge 
				with the recent rise in the cost of employment; and private 
				companies have to step up their game to attract and retain local 
				talent. <br>
				<br>
				A bigger challenge for private enterprise is cost versus 
				productivity, which the public sector is also seeking to tackle. 
				All companies would be happy to absorb the cost if there was a 
				commensurate increase in output and profitability. This is not 
				yet the case and it remains an important policy area for the 
				authorities to support the ambition of the Sultanate competing 
				effectively in a global market place. The population of the 
				country is only 3.1 million, of which nearly 600,000 are 
				non-nationals. In addition, 43 per cent of the population is 
				under the age of 14. This leaves an available working population 
				of only 900,000 people (nationals). We working with a company 
				which had a target, set by the government, of a 95 per cent 
				national workforce. The company developed an approach that has 
				been used here with an independent oil company to help meet and 
				maintain that target and also increase productivity and output.
				<br>
				<br>
				<b><font size="2" color="#000080">Challenges ahead <br>
				</font></b><br>
				The demographic factors outlined previously make 95 per cent 
				localisation a particularly challenging target. Not only are 
				there the usual educational and skills challenges so prevalent 
				in emerging economies, but 15 per cent of the working population 
				is unemployed. Paradoxically, a native technician-level employee 
				can command a 20 per cent salary premium over migrant workers. 
				This has a profound impact on the approach that must be adopted 
				from the outset:<ol style="font-size: 10px; font-family: Tahoma">
							<li>
							<p align="justify" class="style13">Oman does not 
							have limitless resources of manpower; so it is 
							important to maximise output from existing national 
							staff.</li>
							<li>
							<p align="justify" class="style13">If nationals are 
							going to cost more to employ than 
							migrant/non-nationals they have to be at least 
							equivalent to, and preferably higher, in 
							productivity terms than those they replace.</li>
							<li>
							<p align="justify" class="style13">If localisation 
							is going to be sustainable, the business has to make 
							it attractive for non-nationals to train their 
							replacement and then leave, especially in the 
							managerial positions. </li>
							<li>
							<p align="justify" class="style13">A comprehensive 
							training and mentoring programme is needed; operated 
							for nationals by nationals if objectives are to be 
							met.</li>
							<li>
							<p align="justify" class="style13">If a culture of 
							continuous improvement is to be achieved, management 
							needs to win the hearts and minds of the national 
							workforce - otherwise profitability and productivity 
							will suffer.</li>
						</ol>
						<p align="justify" class="style13">The case study is 
						typically an approach at a high level that follows the 
						classic ‘top-down, bottom-up’ approach. Taking the above 
						approach at face value would be to miss some small but 
						very significant details and groundwork that were 
						pivotal to success. The first step was to capture 
						comprehensively, the job specification, role and 
						responsibilities for every one of the major roles in the 
						company and understand how each of these roles impacted 
						the broader company objectives. These company objectives 
						were then reflected in the annual employee development 
						plans and individual goals. This had previously been 
						conducted only sporadically for certain roles but not in 
						a systematic and consistent way. By the end of the 
						project the team had captured a detailed job 
						specification for 26 roles. Next, each national employee 
						was evaluated, based on their skills and performance to 
						date and if found appropriate, a training programme was 
						organised in order to fill the skills shortage needed to 
						get them to the next level.<br>
						<br>
						The general manager of the company was quoted as saying: 
						“If you were to pick any group of workers in the 
						company, it would be impossible to distinguish the 
						Celerant consultants from the employees.” Furthermore, 
						by breaking each job role down to its constituent parts, 
						the supervisory roles historically taken on by 
						non-nationals were demystified and the skills required 
						to do the job were clearly exposed. This enabled a clear 
						and comprehensive skills matrix to be drawn up for each 
						individual as well as one for the trainers to ensure 
						that all the required skills could be delivered by the 
						national workforce themselves. In the example that 
						follows the transformation in the workforce overall 
						added up to more than the sum of the parts.<br>
						<br>
						<b>The principal reasons for this were:</b><br>
						<br>
						By establishing clear roles, skills and responsibilities 
						for each of the key roles, nationals felt confident in 
						taking on those roles.<br>
						<br>
						The adaptable and sensitive approach to training and 
						evaluation linked to the annual development plans and 
						individual goals that related to the companies 
						objectives ensured new working practices were not merely 
						adopted but actively embraced. The workforce had 
						ownership and pride in their work and felt empowered to 
						drive improvement. <br>
						<br>
						<b><font size="2" color="#000080">The situation<br>
						</font></b><br>
						<b>Additional achievements gained by the end of the 
						40-week project included:<br>
						</b><br>
						Offsite leadership training for 55 people.<br>
						<br>
						48 people trained in leadership skills and technical 
						problem solving, including loss accounting.<br>
						<br>
						A cross-discipline, multi-skills training programme was 
						installed to fast track potential managers.<br>
						<br>
						All training was developed so that by the end of the 
						project, all training was carried out for nationals by 
						nationals.<br>
						<br>
						A Supply Chain Training module was developed and 14 
						nationals were successfully trained in key skills.<br>
						<br>
						A ‘train the trainer’ matrix was developed and installed 
						to identify skills gaps.<br>
						<br>
						Whilst the bulk of the attention and effort were 
						focussed on the national employees, the ‘non-national’ 
						community was an important consideration. Localisation 
						could not happen if non-nationals were reluctant to move 
						on. This would result in a ‘bottleneck’ where 
						‘non-nationals’ would be reluctant to hand over to 
						nationals – potentially leading to bad feeling, 
						recriminations and lack of co-operation. As a 
						consequence, non-nationals were given incentives to meet 
						deadlines early; those people who actively mentored 
						nationals would be considered a greater asset and would 
						be more likely to be employed with the company 
						elsewhere. Those who did not, would have a short future. 
						This had the effect of ensuring that the knowledge 
						transfer to the national was as thorough and rapid as 
						possible. In addition, efforts were made to ensure 
						selected non-nationals in key positions had a clear 
						picture of their next job, either in another region or 
						different part of the company.<br>
						<br>
						<b><font color="#000080" size="2">Summary</font></b><br>
						<br>
						Successful nationalisation requires a significant 
						commitment from management to make it happen. Companies 
						should not underestimate the time and commitment 
						required to ensure management positions are successfully 
						localised and productivity increases. Nationals at 
						management level should also be aware that a return on 
						investment is expected and they should be helped, 
						coached, supported and trained to deliver that return. 
						It really is an investment worth making. <br>
