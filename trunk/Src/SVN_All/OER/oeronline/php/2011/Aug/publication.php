				Media Services (UMS) has published the 17th edition of its 
				annual bilingual publication Progress 2011-2012 saluting His 
				Majesty Sultan Qaboos Bin Said on the occasion of the 41st 
				anniversary of Oman’s Blessed Renaissance.<br>
				<br>
				Progress is the first annual country book that is available in a 
				digital version on www.progressoman.com. The digital version 
				offers readers online access to a more comprehensive range of 
				articles than the print edition. It will serve as a one stop 
				reference point for people seeking information on Oman’s 
				remarkable journey over the last 41 years.<br>
				Progress takes an in-depth look at the various sectors that have 
				contributed to the Sultanate’s development and modernisation. 
				The annual country book stands out for its exhaustive analysis 
				of various sectors such as oil and gas, banking, finance and 
				insurance, capital market, education and healthcare, 
				agriculture, retailing, aviation, information and communication 
				technology, real estate, manufacturing, power, infrastructure, 
				tourism etc. Each of these sectors is covered in dedicated 
				chapters highlighting the strides taken by the Sultanate in 
				these areas.<br>
				<br>
				The opening article examines how Vision 2020 has become the 
				country’s blueprint for creating a more self-sustaining, oil 
				independent economy on a par with the rest of the world in all 
				sectors of economy, commerce and trade. Other articles focus on 
				the latest developments, key financial indicators and the future 
				roadmap. They delve into how the Sultanate, guided by the vision 
				and determination of His Majesty, has achieved remarkable 
				progress and development in various fields and established a 
				strong economy by harnessing its natural resources and 
				encouraging private enterprise. The new edition of Progress 
				surveys Oman’s march towards modernity assuming a position of 
				eminence both at the regional and international levels.<br>
