						<p align="justify" style="margin: 3px 10px">
				<strong><font face="Verdana"><span class="style5">
				<span style="font-size: 9pt">NBO’s Women of Excellence 
				Conference provided women with a platform for ‘sharing ideas 
				with great minds’ and encouraged them to embrace positives 
				changes in their work and life</span></span></font></strong><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-inthenews2.jpg" width="514" height="225" style="border: 1px solid #808080"></font><strong><font face="Verdana"><span class="style5"><span style="font-size: 9pt"><br>
				</span></span></font></strong><font face="Verdana" size="1"><br>
				The Women’s Network Group of National Bank of Oman (NBO) 
				recently organised a unique women’s conference, marking the 
				second Omani Women’s Day. The event was held under the auspices 
				of Sayyida Rawan bint Ahmed al Said, NBO board member and 
				managing director and Group CEO, ONIC Holding.<br>
				<br>
				The conference titled ‘Women of excellence’ was held at City 
				Seasons Hotel, Muscat. The initiative was in response to His 
				Majesty’s directive to Omani women to take more active role in 
				the society. The purpose of the conference was to provide an 
				opportunity to groups of women to network and discuss topics of 
				mutual interest.<br>
				<br>
				The conference was well attended by a group of elite women 
				representing the different sectors, in addition to NBO women 
				executives and senior management.<br>
				<br>
				The conference agenda included an opening keynote by chief 
				guest, Sayyida Rawan al Said followed by engaging and 
				enlightening panel discussions, a workshop and video tip on 
				‘women and money’. The panel discussions covered various topics 
				including ‘Breaking the glass ceiling; Can women reach the top 
				of Oman’s largest corporations?’, ‘Work life balance: love your 
				work life’ and ‘Challenges facing businesswomen’, and a 
				de-stressing workshop presented by Mawahib al Shaibani, CEO, The 
				Art of Living.<br>
				<br>
				The one-day programme also included distribution of awards to 
				top performing NBO women junior officers as well as unique art 
				show by the Omani ‘Queen of sand’ and ‘Arabs got talent’ 
				semi-finalist Shayma al Mughairy.<br>
				<br>
				“There is no doubt in my mind that women can be powerful agents 
				of change in Omani corporations,” says Sayyida Rawan, who is one 
				of Oman’s most prominent corporate women. “Though women comprise 
				49.5 per cent of Oman’s population, they make up only 24.4 per 
				cent of the labour force. There is no limit to what women can 
				achieve, but there needs to be more acceptance of women at the 
				workplace. Men’s acceptance of women is very important. We 
				believe <br>
				<br>
				<b>it’s coming but it hasn’t reached its highest level.”<br>
				</b>Commenting on this unique occasion, Salma al Jaaidi, NBO’s 
				AGM — corporate credit risk and president, NBO Women’s Network 
				Group says, “We are delighted to time this event to coincide 
				with Omani Women’s Day, celebrating what women can achieve when 
				barriers are removed. NBO places great importance to empowering 
				women staff by giving them more responsibilities to prove their 
				talents in various roles.”<br>
				<br>
				Aisha al Kharusi, NBO’s head of CSR and corporate communications 
				welcomed the participants saying, “Women now have better control 
				over their lives and can pursue careers of their choice. There 
				is no doubt women can be powerful agents of change in large 
				Omani corporations”.<br>
				<br>
				NBO Women’s Network Group was recently established and consists 
				of dedicated women employee volunteers who want to see change 
				for their peers and create an environment of harmony and 
				transparency.</font><br>
				<font face="Verdana" size="1"><br>
				<br>
				</font><b><font face="Verdana" size="4" color="#C00000">An icon 
				of culture<br>
				</font></b><strong><font face="Verdana"><span class="style5">
				<span style="font-size: 9pt">Royal Opera House Muscat, a 
				world-class performing arts venue, is an iconic symbol of His 
				Majesty Sultan Qaboos Bin Said’s dedication to the cultural 
				development of the Sultanate</span></span></font></strong><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-inthenews3.jpg" width="514" height="244" style="border: 1px solid #808080"></font><strong><font face="Verdana"><span class="style5"><span style="font-size: 9pt"><br>
				</span></span></font></strong><font face="Verdana" size="1"><br>
				His Majesty Sultan Qaboos bin Said presided over the official 
				opening of the Royal Opera House (ROH) Muscat recently, 
				unveiling a stupendous cultural icon for the Sultanate. <br>
				<br>
				His Majesty was received by His Highness Sayyid Fahd bin Mahmoud 
				Al Said, deputy prime minister for the Council of Ministers, and 
				chairman of the Supreme Committee of ROH. Later, His Majesty cut 
				the ribbon marking the official opening of the ROH. The band of 
				Royal Guard of Oman played the national anthem. The ceremony was 
				also attended by Sheikh Nasser Al Mohammed Al Ahmad Al Sabah, 
				prime minister of Kuwait, Their Highnesses, chairmen of the 
				State Council and Majlis Al Shura, Their Excellencies, senior 
				officials and ambassadors of countries accredited to the 
				Sultanate and other dignitaries.<br>
				<br>
				The landmark building is an iconic symbol of His Majesty’s 
				ongoing dedication to support the cultural development of the 
				Sultanate and a commitment to global outreach and dialogue 
				through the arts. Furthermore, this historic cultural effort is 
				unprecedented in the region: a world-class performing arts venue 
				that will serve as an incubator for creativity and innovation 
				for generations to come.<br>
				<br>
				Giacomo Puccini’s Turandot (tale of a frigid Chinese princess 
				named Turandot) was the first opera at the newly inaugurated ROH. 
				This opera was directed by Franco Zeffirelli, conducted by 
				Placido Domingo and performed by the Fondazione Arena di Verona 
				from Italy.<br>
				<br>
				The opening of the ROH also heralded a colourful season of 
				operas and concerts by esteemed international troupes. The 
				inaugural season of the ROH which is scheduled from 14 October 
				to the end of March, 2012, features two outstanding operas -- 
				Puccini’s Turandot and Bizet’s Carmen. Like the great opera 
				houses of the world today, ROH Muscat will present a broad 
				spectrum of programmes in the performing arts – operas, music 
				concerts, ballet and dance, theatre, dialogue. This season 
				presents four ballets including the classic favourites, Don 
				Quixote, Giselle and Swan Lake.<br>
				<br>
				Throughout the inaugural season, ROH will host various 
				activities including open houses for the public featuring free 
				performances; backstage tours; pre-performance talks with 
				artists and experts in the fields of music and opera; and 
				workshops for children and adults. The supreme committee has 
				engaged the John F Kennedy Centre for the Performing Arts to 
				provide interim executive leadership.<br>
				<br>
				With a seating capacity of well over 1,000 for theatre 
				productions and some 600 for musical performances – and 
				cutting-edge technologies for stage mechanics, sound and 
				lighting, as well as a costume department, the facility is 
				capable of implementing the world’s most sophisticated 
				productions. Presently almost all the technicians are top 
				professionals from abroad, but a plan is in place to build 
				capacity with local skills and talent.<br>
