						<font face="Verdana" size="4" color="#C00000">Luring 
						investors</font></b><p align="justify" style="margin: 10px">
				<strong><font face="Verdana"><span class="style5">
				<span style="font-size: 9pt">State-of-the-art conference and 
				exhibition facilities to be complemented by four hotels at the 
				Oman Convention and Exhibition Centre</span></span></font></strong><p class="style15" align="center">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-inthenews1.jpg" width="514" height="263" style="border: 1px solid #808080"><br>
				</font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">Oman Investment Forum 20011 
				highlighted the outstanding investment opportunities for 
				companies based in Oman and prospective international investors 
				looking for an excellent return on investment and solid 
				long-term growth in a dynamic and rapidly growing economy<br>
				<br>
				Leading government decision makers and senior private sector 
				executives gathered at the Oman Investment Forum 2011 recently 
				to debate the growing number of investment and business 
				opportunities in the Sultanate’s rapidly growing market. The 
				three-day forum was organised by Middle East Business 
				Intelligence (MEED), in partnership with the Public Authority 
				for Investment Promotion &amp; Exports Development – Oman (PAIPED) 
				at the Al Bustan Palace Hotel.<br>
				<br>
				In a mix of presentations and panel discussions, delegates were 
				involved in debates on a range of areas related to project 
				development and opportunities across Oman’s major sectors 
				including funding and financing; transport; tourism; utilities; 
				downstream development, oil and gas and social infrastructure. 
				Minister of Commerce &amp; Industry, HE Shaikh Saad bin Mohammed Al 
				Saadi, Minister of Finance, HE Darwish Al Balushi and Minister 
				of Sports Affairs &amp; Chairman of Information Technology 
				Authority, HE Ali Masoud Al Sunaidy, delivered keynote 
				ministerial speeches at the forum. The event was hosted by 
				Edmund O’Sullivan, chairman of MEED Events.<br>
				<br>
				Dwelling upon the importance of the forum, MEED Chairman, Edmund 
				O’Sullivan cited a number of key statistics that are set to 
				underpin the growth of Oman’s major sectors in the next five 
				years. “Middle East’s Islamic finance industry worth RO8Obn is 
				now open to Oman’s banking sector; moreover, RO8bn is estimated 
				to be spent on transport infrastructure projects including major 
				roads, airports, port and railway,” he says. “While RO1.92bn 
				will be invested in the Duqm refinery plans to be built in the 
				next five years, RO300mn will be tendered for desalination water 
				projects and RO148mn will be invested in the new aluminum 
				facility at Sohar over the next 31 months.”<br>
				</font><font face="Verdana" size="2" color="#CC3333"><b><br>
				Two power IPOs </b></font><font face="Verdana" size="1"><br>
				Oman is planning to hold two initial public offerings (IPO) for 
				power projects in the next two years, Bob Whitelaw, the chief 
				executive at state-run Oman Power and Water Procurement Co (OPWP), 
				has informed the forum. The first IPO, planned for April 2012, 
				is for 35 per cent of the 2 GW Sur power plant due to cost 
				$1.5bn and to be commissioned in 2014. Phoenix Power Co won a 
				25-year build-own-operate agreement for the plant last year. 
				Phoenix Power is a consortium of Marubeni Corporation, Chubu 
				Electric Power Co Inc, Qatar Electricity and Water Company and 
				Oman’s Bahwan Engineering Group. The second IPO, planned for 
				April 2013, is for 35 per cent in the Batinah Power Company, for 
				which the government has yet to close a deal with developers. 
				Last month, SMN Power Company floated 35 per cent of its share 
				capital through an IPO. Four power companies already trade on 
				the Omani bourse. </font>
