						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="4" color="#C00000"><b>Seize 
						the opportunities</b></font><p align="justify" style="margin: 3px 10px">
						<span class="style14"><strong>
						<font face="Verdana" size="1"><br>
						</font><font face="Verdana" style="font-size: 9pt">
						Property investors in Oman must look at the drivers of 
						the real estate market in the Sultanate instead of 
						worrying about the global economy<br>
						</font><font face="Verdana" size="1"><br>
						</font></strong><font face="Verdana" size="1"><i>
						<font color="#333333"><strong>By sudhakar reddy<br>
						</strong></font></i><strong style="font-weight: 400">
						<br>
						<br>
						Investors all over the world are nervous as, to quote 
						The Economist, they have nowhere to hide. Every asset 
						class is taking a beating and investors are flocking to 
						buy US treasury bonds in spite of the downgrading of US 
						government debt by Standard &amp; Poor’s, and miserly 
						interest rates on these bonds. While the bursting of the 
						real estate asset bubble and the banking crisis that 
						followed caused a major shock in 2008, what is causing 
						nervousness now is the sovereign debt crisis especially 
						in the Euro zone. Investors are hesitant to look at any 
						asset class.<br>
						<br>
						In line with global sentiment, property investors in 
						Oman have become ultra cautious. They are focusing on 
						the global crisis rather than looking at the 
						fundamentals of the real estate market in Oman. While 
						all economies in the world are interlinked, different 
						economies are likely to perform differently and we must 
						look at the drivers of the real estate market in Oman 
						and of the local economy.<br>
						<br>
						Oman economy is still, significantly, driven by oil 
						prices. The average price per barrel was about $76 in 
						2010. So far this year the average price per barrel of 
						Oman crude is slightly over $100 per barrel. The 
						government has thus enough revenues to power the economy 
						forward. The sovereign debt crisis has not impacted oil 
						prices so far. However, if there is a serious crisis and 
						the world economy tips back in to a recession, oil 
						prices may go down, but this is unlikely to happen as 
						supply of oil is not growing faster than demand.<br>
						<br>
						Ultimately, the real estate market in any country is 
						driven by job creation and incomes. The private sector 
						in Oman created over 80,000 jobs in the whole of 2010. 
						In the first eight months of 2011, the private sector 
						has created more than 85,000 jobs. Thus the growth this 
						year has been faster than last year and we can expect 
						employment in the private sector to grow by over 10 per 
						cent this year. In addition, the government is expanding 
						and creating more jobs in the government sector. Per 
						capita incomes have been rising too. This is driving 
						demand for real estate and rents remain firm. Of course, 
						job creation in the private sector is driven by 
						government spending and considering that oil prices are 
						running much higher than budgeted, we can expect the 
						economy to grow faster than anticipated.<br>
						<br>
						Interest rates on deposits are abysmally low and 
						equities are giving negative returns. However, investors 
						can earn over 8 per cent from real estate in Oman. 
						Property developers can earn about 10 per cent. In 
						addition, properties are a hedge against inflation and 
						investors can expect capital appreciation at least in 
						line with inflation, if not more. In the current 
						context, these are very high returns. Thus property 
						investors in Oman are missing a huge opportunity by 
						sitting on the sidelines and worrying about the global 
						economy instead of focusing on the factors that affect 
						the real estate market in Oman and seizing the 
						opportunities.<br>
