						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" color="#C00000" size="4">Adapt and 
						innovate<br>
						</font></b><strong style="font-weight: 400">
						<span class="style27"><font face="Verdana" size="1"><br>
						</font></span></strong><b>
						<font face="Verdana" style="font-size: 9pt">
				<img border="0" src="images/nov-interview1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="120" height="256" align="right"></font></b><span class="style27"><font face="Verdana" style="font-size: 9pt"><strong>Mohanbir 
						Sawhney, McCormick Tribune Foundation Professor of 
						Technology at the Kellogg School of Management and board 
						member, Bahwan CyberTek, was in Muscat recently to 
						address a seminar titled ‘Competing on Agility.’ Mayank 
						Singh spoke to him on the sidelines of the conference</strong></font><strong style="font-weight: 400"><font face="Verdana" style="font-size: 9pt"><br>
						</font><font face="Verdana" size="1"><br>
						<br>
						</strong><strong>What is the central premise of the 
						topic, ‘Competing on Agility – Building a Resilient 
						Enterprise’?<br>
						</strong><strong style="font-weight: 400">The basic 
						thesis of agility is that we are faced with a structural 
						increase in the level of volatility with factors that 
						are beyond our control. These include macro economic 
						factors like the recession, we saw the meltdown of the 
						financial markets, the problem with the Euro, Greece and 
						so on and that’s one driver of agility. Then there are 
						regulatory shocks that are a result of macro-economic 
						changes. For example, in the wake of the financial 
						meltdown there were regulatory changes that dramatically 
						affected financial services and their profitability. 
						Apart from these there are technological shocks like 
						social media, smart phones, the coming up of mobile 
						devices and the internet. Changes in consumer 
						preferences can catch companies completely by surprise. 
						When you add all of this up and look at it as a CEO or a 
						business leader then you are dealing with an environment 
						where there is a lot of unpredictability.<br>
						<br>
						Moreover, it is not going away and you cannot actually 
						manage or reduce it. So what do you do in such a 
						situation? My thesis is that the fundamental strategic 
						response to this scenario is to create an organisational 
						enterprise that is able to deal with such volatility 
						better. The best way to deal with volatility is by 
						becoming adaptable. Agility is a strategic response to 
						volatility and uncertainty. An agile enterprise is an 
						organisation that is able to sense and respond faster 
						and more intelligently relative to competition.<br>
						<br>
						</strong><strong>In the Middle East, government 
						regulations restrict agility and the ability of 
						companies to respond. Given this scenario what can be 
						done by organisations that are bound by such 
						constraints?<br>
						</strong><strong style="font-weight: 400">I would 
						respond to this in two ways -- one, the same 
						conversation that we are having about agility at the 
						enterprise level needs to be applied at the policy level 
						and to the government. Thus it is important for the 
						government to look at agility at the macro level. For 
						example how quickly can Oman as an economy adopt to 
						these changes. Ultimately as a country you are competing 
						globally and more so in the Gulf region. If you look at 
						aviation and tourism, Oman’s infrastructure is lagging 
						compared to an Abu Dhabi, Qatar, Bahrain or Dubai. So 
						Oman needs to upgrade its infrastructure. Secondly, you 
						do what in engineering terms is called ‘constrained 
						optimisation’, or you work within the constraints and 
						see the best that can be done. Obviously, this is not an 
						optimal solution but there are still things that can be 
						done within the degrees of flexibility that is 
						available.<br>
						<br>
						</strong><strong>In your writings you talk about famous 
						mistakes stating that the way to measure a failure is 
						how much you learn from it. How important is it for 
						companies to learn from their errors?<br>
						</strong><strong style="font-weight: 400">We spend a lot 
						of time analysing best practices, and these teach us 
						about success, which is a good thing as one needs to 
						learn from other people’s successes, but we do not 
						devote enough time talking about failures. I call them 
						famous because these are extremely valuable lessons that 
						one can learn from mistakes. We forget that success 
						actually is a pretty bad teacher and failure is a pretty 
						good one. We do not look at our mistakes because it is 
						painful to look at them. The only true failure is the 
						failure to learn from your mistakes.<br>
						<br>
						</strong><strong>You say that this is the second gold 
						rush in the digital space, with valuations not being in 
						sync with the fundamentals. What makes you so skeptical 
						about recent valuations?<br>
						</strong><strong style="font-weight: 400">I get a 
						feeling of déjà vu, as eyeballs have somehow became a 
						proxy for profits. There is a feeling that just because 
						an enterprise has millions of customers, it is going to 
						figure out a way of making money out of them, but this 
						is not true. To some extent, I can understand that a 
						franchise like Facebook with 800 million will figure out 
						ways to monetise that base, but if you look at 
						businesses like Twitter or LinkedIn or Groupon, I am 
						very skeptical about the sustainability of their revenue 
						model and the valuations that are being ascribed to 
						them. A $20bn valuation for Groupon is clearly not 
						sustainable as the company is not profitable. We tend to 
						get carried away by the moment, so today if you put 
						social or mobile into your business plan then you can 
						get unlimited amounts of funding and valuations and 
						that’s what I am concerned about. I agree that a company 
						can invest ahead of revenues or invest to create scale 
						like an Amazon or Facebook has but at some point it 
						needs to revert to fundamentals and that’s not 
						happening.<br>
						<br>
						</strong><strong>There is a lot of thrust on SME 
						development in Oman and the Middle East region. How can 
						start ups be successful?<br>
						</strong><strong style="font-weight: 400">A start-up or 
						small business needs to have a very clear understanding 
						of what customer problem it is solving. Entrepreneurs 
						are passionate and they have a strong belief in what 
						they are doing, but they need to remember that they are 
						not the customer. An enterprise exists to solve a 
						particular problem for a customer. An entrepreneur needs 
						to have a very clear articulation of the customer 
						problem that he is solving, why his solution is better 
						than what customers might consider and what evidence he 
						has that customers are going to pay enough to justify 
						his business. So that is the first set of issues. Then 
						SME’s need to concentrate on building a team that has 
						complementary capabilities and skills, because a number 
						of times entrepreneurs tend to hire people who are like 
						them, when actually they need people who can complement 
						their strengths and weaknesses. Beyond that it is just 
						target persistence and pathological optimism. In Oman, 
						you need to come up with a product that is relevant to 
						the Omani market but you also need to keep an eye 
						towards the broader GCC market because unfortunately in 
						some businesses, Oman does not have a big enough market. 
						Once you get to a certain scale then you start getting 
						restricted by the market here.<br>
						<br>
						</strong><strong>There is a renewed focus on job 
						creation in the Middle East market. What is your advice 
						to young people?<br>
						</strong><strong style="font-weight: 400">As you start 
						out on your education and career you should experiment, 
						you should probe different things. The young generation 
						today has much more choice and freedom, so you should 
						test different hypothesises, but once you get to a 
						certain stage, probably three to five years into your 
						career, then you should discover your passion and then 
						stop experimenting. The problem is that sometimes your 
						passion finds you and so one needs to keep probing and 
						experimenting. Once you find something that resonates 
						then one should lock down on that passion by becoming an 
						expert or a specialist. A combination of passion, deep 
						knowledge and expertise in an area will drive success. 
						Focus is not about what you focus on but the 100 things 
						that you don’t do, like Steve Jobs used to say, ‘People 
						know us for the five things that we have done and not 
						for the 100 things that we have not done’. Don’t spread 
						yourself too thin, understand your expertise and build 
						on it till you are the world’s best in what you have 
						decided to pursue. That’s why finding the groove is 
						important. <br>
						<br>
