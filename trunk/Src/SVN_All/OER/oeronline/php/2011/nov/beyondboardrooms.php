				Ambuj Khare, Senior General Manager, General Electric &amp; Trading 
				Co (Genetco), there is no such thing as beyond the boardroom. “I 
				believe that whatever you do at home, it all comes back to the 
				professional front in a refined way. For example, at home you 
				are contributing to something or someone. Similarly, in the 
				office, your contribution to the team is important.”<br>
				<br>
				According to Ambuj, everybody needs to constantly educate 
				themselves in order to keep abreast of the latest professional 
				practices so that their contribution to the organisation can be 
				valuable. That is the key to success. “We are like machines 
				which have to be serviced often. We too need to keep upgrading 
				ourselves and then only can we deliver,” he says in a very easy 
				going manner. For this very reason, he is extremely impressed by 
				the Management style of Dr Muttanah, CEO of Genetco, who 
				believes in open door policy and continuous self-development 
				through experimentation.<br>
				<br>
				For this precise reason, Ambuj is presently involved doing a 
				Doctorate thesis on 3rd Party Logistics (3PL) in the Middle 
				East. And with Middle East being a trading hub, a PhD in 
				logistics will help him overcome the challenges. He is expected 
				to submit his last paper next year.<br>
				While talking about education, he discloses that he invests a 
				certain portion of his earnings every year for education. His 
				wife who is a CIPD qualified HR professional and is a 
				performance &amp; development manager is also studying for a higher 
				level of CIPD qualification, whilst working.<br>
				<br>
				Ambuj, a graduate of Delhi University in India, went on to do 
				his MBA and masters in tourism management. He worked with 
				Videocon and Onida before he joined Genetco two years ago. 
				Initially, he wanted to become a theatre personality while 
				studying college in India but one thing led to another and now 
				he is into the consumer electronics business.<br>
				<br>
				<b>Cultural knowledge<br>
				</b>“I was general manager with Onida for their all India 
				operations. Then later in 2001, I shifted to the Middle East to 
				manage Onida business in MEA from Dubai. Then I was vice 
				president with Videocon for MEA region and now I have been 
				working in Oman for the past two years,” Ambuj informs.<br>
				<br>
				Knowing about the culture and tradition of the region you are 
				working in, is important as it helps you to grow your business, 
				opines Ambuj. “I feel good when I have knowledge about the local 
				culture. Then you are moulding yourself to adapt to different 
				situations,” he adds. He has friends who hail from different 
				countries like Oman, Scotland, Egypt, Syria and Lebanon and they 
				have outings on weekends to different places. Ambuj’s love for 
				cooking helps him to try out cuisines of different countries and 
				enjoy the weekend bliss. He is fond of travelling and has 
				visited many countries both for business and pleasure. He also 
				loves to read autobiographies of great personalities which again 
				help him to understand different cultures and opinions.<br>
				<br>
				“I enjoy life and have no regrets. I take things as they come. 
				Tensions will remain, but success lies in the way how you deal 
				with these tensions whilst not being distracted by negatives,” 
				says Ambuj while summing up his philosophy of life.<br>
				<br>
				Some of the people who influenced him in his life are K R Kim, 
