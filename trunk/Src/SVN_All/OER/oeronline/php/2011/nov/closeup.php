						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Saudi Arabia reaps fruits of smart planning</font></b><p align="justify" style="margin: 10px">
				<font face="Verdana"><br>
				</font>
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-closeup.jpg" style="border: 1px solid #808080" class="style4" width="200" height="267" align="right" hspace="8" vspace="4"></font><font face="Verdana"><font style="font-size: 9pt"><b>Saudi 
				Arabia was ranked number 17 on the recently released Global 
				Competitiveness Index (GCI) which places the Kingdom ahead of 
				France, Australia and Malaysia<br>
				</b></font><font size="1"><br>
				Numerous indexes and reports released in the recent past give 
				high marks to Saudi economy in recognition of its exceptional 
				performance — and for good reasons. This is true of the recently 
				released Global Competitiveness Index (GCI) and a forecast by 
				the International Monetary Fund.</font></font><p align="justify" style="margin: 10px">
				<font face="Verdana"><font size="1">For instance, Saudi Arabia 
				was ranked number 17 on 2011-2012 version of GCI, published by 
				the World Economic Forum. Only Qatar outperformed Saudi Arabia 
				on GCI within the Gulf Cooperation Council (GCC). The ranking 
				places Saudi Arabia ahead of France, Australia and Malaysia, to 
				name a few countries.<br>
				<br>
				In addition, Saudi Arabia achieved ranking number 33 on 
				2010-2011 of Network Readiness Index (NRI), a cornerstone of the 
				Global Information Technology Report. The World Economic Forum 
				together with INSEAD, the international graduate business school 
				and research institution, produce the annual report. The NRI 
				index relies on some 68 variables, of which 27 items are hard 
				data such as number of personal computers and internet 
				penetration rate. The UAE leads the Arab countries on the back 
				of achieving ranking number 24 amongst 138 countries ranked in 
				the report. The achievement is testimony of steady appreciation 
				of information and communication technologies (ICT) in GCC 
				states at large.<br>
				<br>
				Still, chances are that Saudi Arabia would be able to improve 
				its ranking on ICT thanks to steady investment on higher and 
				technical education. The kingdom’s 9th development plan covering 
				the period 2010 to 2014 stipulates developing 25 technical 
				colleges and equipping many other institutes with latest 
				technological means. The plan fits the goal of ensuring that 
				Saudi nationals are equipped with the latest technological 
				means, and thus ready to confront job market challenges.<br>
				<br>
				Of $385bn comprising the 9th development plan, some 50.6 per 
				cent is allocated for human resources development, 19 per cent 
				for social development and health, 15.7 per cent on economic 
				resources, 7.7 per cent for transportation and 
				telecommunications and 7 per cent on municipality services and 
				housing projects.<br>
				<br>
				<b>Foreign investments <br>
				</b>Furthermore, Saudi Arabia is second to none amongst GCC, 
				Arab and all countries located in West Asia when it comes to 
				attracting foreign direct investments. More specifically, the 
				kingdom enticed some $28.1bn worth of inward FDI in 2010, a 
				whopping 48 per cent of total inflows in the West Asia region.<br>
				<br>
				This extraordinary achievement is the result of Saudi Arabia’s 
				economic reforms and its accession to World Trade Organisation 
				(WTO) in December 2005 following decade-long negotiations. It is 
				the last GCC country to join the WTO. Saudi Arabia’s foreign 
				investment law (FIL), which was enacted in April 2000, grants 
				foreign firms the right to own majority stakes in companies 
				within the kingdom. It also allows foreign banks to operate in 
				the form of locally incorporated joint stock companies or as 
				branches of international financial institutions. In addition, 
				Saudi Arabian General Investment Authority (Sagia), which looks 
				after foreign investments, has put in place a one-stop-shop 
				application process and a 30-day deadline for decisions on 
				investment applications. At $8.1bn, Qatar achieved the second 
				best FDI inflows amongst Arab countries, but certainly with a 
				distant figure compared to that of Saudi Arabia.<br>
				<br>
				<b>Economic Growth <br>
				</b>With regards to economic growth, the IMF projects a gross 
				domestic product (GDP) growing by 6.5 per cent in 2011. 
				Undoubtedly, the figure compares favourably with those of 4.1 
				per cent in 2010 and merely 0.1 per cent in 2009. The statistics 
				are in real terms, adjusted for inflationary pressures. Like 
				elsewhere within the GCC, inflation is under control in Saudi 
				Arabia, at least in the current circumstances. The steady GDP 
				growth partly reflects positive spill-over effects of special 
				appropriation of $130bn announced in the first quarter of 2011. 
				Of this, some $47bn and $35bn are reportedly allocated for 2011 
				and 2012, respectively. Certainly, this amount is in addition to 
				budgetary spending of $155bn for fiscal year 2011.<br>
				<br>
				King Abdullah revealed the special spending following his return 
				home from medical treatment abroad, which coincided with the 
				Arab Spring. The spending package is designed to address 
				challenges such as unemployment, which stands around 9 per cent 
				amongst the nationals.<br>
				<br>
				Clearly, the results suggest that the Saudis are reaping the 
				fruits of smart planning and hard economic work of the last few 
				years.</font> </font></td>
