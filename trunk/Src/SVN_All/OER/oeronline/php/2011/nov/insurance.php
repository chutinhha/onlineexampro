						<td valign="top">
						<p align="justify" style="margin: 10px">
				<b><font face="Verdana" size="4" color="#C00000">Channels of 
				growth<br>
				</font></b><font face="Verdana" size="1"><br>
				<img border="0" src="images/nov-insurance1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="100" height="121" align="right"></font><strong><font face="Verdana"><span class="style5"><span style="font-size: 9pt">Al 
				Ahlia and Royal Sun Alliance, two of the big insurance companies 
				in Oman merged sometime ago. Lloyd East, Managing Director of Al 
				Ahlia Insurance Co – the merged entity – talks about his aims 
				and goals for the company in a candid interview with Mayank 
				Singh and Visvas Paul D Karra<br>
				</span></span></font></strong><font face="Verdana" size="1"><br>
				<b>Can you briefly describe the merger of Al Ahlia and RSA? Does 
				the culture of a bigger company dominate?<br>
				</b>We completed the physical integration of Al Ahlia and RSA 
				Oman under my predecessor within a year, and ahead of schedule. 
				I assumed my role as CEO of the combined entity Al Ahlia in 
				March 2011. The key to this integration has been in blending the 
				strengths of Al Ahlia’s local expertise and branch network with 
				RSA’s strengths of underwriting and technical capability. We 
				believe the combined entity gives us a unique proposition to 
				offer to the market. <br>
				<br>
				As to which culture is dominant, then obviously both Al Ahlia 
				and RSA have unique skills and capabilities and hence the 
				integration involved ensuring these were retained and built into 
				the new entity going forward. Al Ahlia’s national footprint and 
				local knowledge are essential to make our service network 
				tailored to the needs of our customers. It enables the combined 
				business to offer a national service proposition to a new 
				segment of customers. If you look at my Executive Team, you will 
				also notice that it is a combination of former RSA and Al Ahlia 
				Executives. <br>
				<br>
				Being part of RSA Group plc, one of the oldest and largest 
				insurance companies in the world, we have access to capacity, 
				capability and expertise that a local player would be unable to 
				replicate. Therefore whilst the integration is not an identical 
				culture to either of the parent companies, it draws strongly on 
				RSA’s principles and values but uses the local knowledge and 
				expertise that Al Ahlia had built up over many years to guide 
				the way we conduct our business in Oman.<br>
				<br>
				<b>Is this your first venture in the Middle East?<br>
				</b>Yes and no… I have been in retail financial services for 
				over 20 years, working for a number of blue chip financial 
				services companies such as Bank of Scotland, MBNA credit card 
				company and Royal Bank of Scotland. I was also managing director 
				of a 50-50 joint venture between the Automobile Association and 
				Halifax Bank of Scotland which had more than 14 million 
				customers in the UK. Thereafter, I was part of a start-up 
				venture setting up Western European’s first Takaful Insurance 
				Company that involved exposure to the Middle East for 
				fundraising and shareholder discussions. Subsequently I joined 
				RSA two years ago. <br>
				<br>
				Joint venture experience is clearly very relevant to the current 
				integration exercise. Then, working within RSA helped me 
				understand its vision and culture along with the organization’s 
				processes and the technical rigour. I think that makes me well 
				placed to understand the RSA ‘way’ and blend that successful 
				formula with the skills and capabilities that Al Ahlia had built 
				up.<br>
				<br>
				On a personal note, however, it is my first relocation to the 
				Middle East and my family have settled extremely well. We have 
				been very enthused by the warmth of the welcome, both by the 
				team here at Al Ahlia and the people of Oman. <br>
				<br>
				<b>What is your vision to take Al Ahlia forward?<br>
				</b>We have established a tremendous platform and are well 
				placed to build our position. Recent business performance 
				suggests that we are on the right track. We have reorganised our 
				sales and technical teams around our customers. <br>
				<br>
				We have developed strong IT platforms and controls that we 
				deploy at our branches and with our partners to efficiently and 
				safely grow our business. We are now extending this capability 
				to our brokers and motor scheme partners. Our technical and 
				pricing capabilities also offer us a competitive edge in a 
				saturated market. <br>
				<br>
				But our biggest asset is our people. Integrations can be tough, 
				but we have made real efforts to build new relationships, 
				strengthen employee engagement; and people tell me that a new 
				level of energy is being released by the team. This has included 
				a host of social events - Iftar parties, football matches, 
				barbeques, charity fundraising, quarterly meetings of all staff 
				(now infamously named Dardasha!) and my personal favourite, the 
				Al Ahlia cook-off! (Each of us contributed a homemade dish and 
				the proceeds went to charity.)<br>
				<br>
				<b>In Oman insurance penetration is not big. How do you grow in 
				this market?<br>
				</b>We are not going to wait for the market to grow. We’ve had 
				two great consecutive quarters and believe that if we apply the 
				right propositions and our service delivery is strong, then we 
				can grow. That is not to say we are without areas for 
				improvement – but the team here have made huge strides in 
				rebuilding momentum in the current environment. <br>
				<br>
				That said, some underlying economic factors are reasonably well 
				poised and we should see some natural growth in the coming 
				years. We’ve geared up to grow our personal lines business by 
				improving the quality of our service, are developing our product 
				mix and are strengthening our branch footprint. But we will be 
				careful that our pricing does not dictate the growth and quality 
				of the portfolio. We want the risk quality of our portfolio to 
				dictate our pricing, rather than use soft rates to grow our 
				portfolio. As evidence of the pricing challenge, rates post-Gonu 
				cyclone actually came down. Equally, motor pricing is highly 
				competitive in Oman. That is why we adhere to the RSA maxim of 
				Sustainable Profitable Growth. Ultimately our clients rely on us 
				when they need us most at the point of a claim. With Al Ahlia 
				and RSA, they know we have financial strength, we can service 
				the most challenging of claims and we will base our growth 
				strategy on a firm footing to ensure we are here for the long 
				term. <br>
				<br>
				<b>Apart from strong technical underwriting skills, what are 
				your other strengths?<br>
				</b>We have three strengths. First, the Al Ahlia branch network 
				is a huge asset when developing new propositions as we can 
				service a national customer base. <br>
				<br>
				Second is our service. We lost a little service quality during 
				integration but service is now improving and we hear clients 
				saying that Al Ahlia with RSA has come back strongly. <br>
				<br>
				Third, as part of a global company operating in 34 countries and 
				providing products and services in over 130 countries, we have 
				capacity and the global expertise to partner with the most 
				complex of clients. <br>
				<br>
				<b>What are the future opportunities and challenges?<br>
				</b>Clearly insurance penetration is not as high as other 
				countries. And over a period, that ought to yield greater 
				opportunities for a personal lines and commercial lines insurer.<br>
				<br>
				Having completed the integration and harnessed the strengths of 
				both organisations, I have a first class team that I am 
				confident will build a long-term, sustainable and profitable 
				position in the market. Having launched a Takaful player myself, 
				it is hard not to be intrigued by the launch of Takaful. 
				Equally, the strong economic growth of 2008 will probably not be 
				repeated so that sustainable profitable growth will be more 
				challenging. <br>
				<br>
				But for my family and me, the commercial and personal learning 
				combined with the pleasure gained by living and working in such 
				a wonderful country means the opportunities far outweigh the 
				challenges.<br>
				</font></td>
