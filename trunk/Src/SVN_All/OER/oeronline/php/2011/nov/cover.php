						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The great mismatch</font></b><p align="justify" style="margin: 10px">
				<font face="Verdana"><b><font style="font-size: 9pt">Equipping 
				young Omanis with the requisite job skills is an opportunity and 
				challenge for the government, private companies and training 
				institutes. The way this issue is dealt will determine the 
				course of the Sultanate’s economy. A report by Mayank Singh<br>
				</font></b></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-cover01.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				The Polyglot Institute Oman screened 300 aspirants for a 
				National Funded Programme (NFP) recently. NFP is a government 
				initiative to promote employment for the national work force. 
				The system works as follows – a company gives an undertaking to 
				recruit a certain number of Omanis, after they have been trained 
				by a vocational or technical training institute. The training 
				institute in association with the company selects the requisite 
				number of people and submits it to the Ministry of Manpower, 
				which in turn pays the institute to train these job aspirants. 
				Post training, these candidates join the company’s workforce. 
				Geetha Shankar, director, Business Management Studies, Polyglot 
				says, “Out of the 300 people that we interviewed we could not 
				get even 20 people to fulfil the primary requirements of the 
				company.”<br>
				<br>
				One hears the same refrain about a mismatch between employer 
				needs and employee skills from various companies. Says Dr Brian 
				Buckley, general manager and CEO of Oman LNG, “Whenever we 
				advertise for 100 jobs on the technical side we get a few 
				hundred applications, while a similar number is put out on the 
				nontechnical side, there are thousands of resumes that come in. 
				Overall, there is a clear deficit of skills on the technical 
				side and that needs to be bridged.” As the base of the economy 
				expands and new industries are set up, there is a growing demand 
				for people with better skills. This is not a problem unique to 
				Oman; high unemployment coexists with a dearth of skilled and 
				talented people, all over the world.<br>
				<br>
				Kumail Baqer Al-Moosawi, director of people, Nawras takes the 
				argument further, “What is lacking in Oman today is 
				specialisation; for example, there are students with legal 
				skills but there are specialised fields like contracts in which 
				there are a handful of lawyers. We find it difficult to find 
				engineers with a Wi-Max or backbone engineering background. 
				Overall, there is a need to shift from general to specialised 
				areas.”<br>
				<br>
				<img border="0" src="images/nov-cover02.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>Job seekers galore <br>
				</b>If the Sultanate can make young Omanis readied for work; if 
				it can train its young to man global standard factories; if it 
				can get its young to be smart accountants or IT professionals; 
				if it can train its young into efficient yet friendly front 
				office staff at super markets; if it can produce enough nurses 
				and doctors to charm and heal the world’s increasingly old, then 
				it can alter the course of its future.<br>
				<br>
				A back-of-the-envelope calculation shows that roughly 40,000 job 
				seekers join the market every year. This translates into 200,000 
				aspirants over the next five years. If this resource base could 
				be used optimally it could lead Oman’s economy to new heights. A 
				better employed work force would translate into a more 
				prosperous nation.<br>
				<br>
				This presents a never-before challenge and opportunity for 
				vocational and technical training companies in the Sultanate. 
				Says Robert MacLean, principal, National Hospitality Institute (NHI), 
				“There is no question about the business opportunity that 
				training for the private sector provides in Oman.”<br>
				<br>
				<img border="0" src="images/nov-cover03.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>Importance of training<br>
				</b>The Omanisation programme which started in 1988 with an 
				effort to replace expatriates with trained Omani personnel 
				provided an impetus to the training industry in Oman. The Vision 
				2020 document in June 1995 identified skilling of the Omani 
				workforce and developing human resources as one of its major 
				priorities. The training industry’s size in Oman is estimated at 
				RO5mn and is expected to grow to RO10mn over the next five 
				years. The training industry today boasts of 78 vocational 
				training centres, ten technical colleges, five colleges of 
				applied science, an International Maritime College of Oman and a 
				Tourism College.<br>
				<br>
				Training institutes offer a variety of courses and skills in 
				languages, retailing, tourism, IT, accountancy, hospitality, 
				manufacturing and industrial activity. These courses come at 
				various price points based on the duration and specialisation. 
				Polyglot offers training from 10 to 600 hours in the RO80-1,000 
				price range. The NHI’s NFP training ranges from five months to 
				eight months, while those for the private sector can be 
				customised as per their requirements.<br>
				With college education being beyond the reach of a number of 
				Omanis, these institutes serve as a bridge between students who 
				have a higher secondary qualification and the job market. These 
				people can spend anywhere between RO300-500 at a training 
				institute to get a certification or skills and then seek job 
				opportunities.<br>
				<br>
				<img border="0" src="images/nov-cover04.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>Training the trainer<br>
				</b>Says Al Qassim Al Harthy, executive director, Polyglot, 
				“Today, things are different from what they used to be 10-15 
				years ago. Companies expect graduates to be equipped with the 
				requisite skill sets before they join them.” As new sectors open 
				up and the market becomes more demanding, training and 
				vocational institutes have themselves gone in for upgrades and 
				tie ups. Polyglot has recently tied up with SAP to provide its 
				courses in Oman. It is also working on a partnership with the 
				Chartered Institute of Logistics and Transport (CILT), UK. The 
				CILT is a pre-eminent independent professional body for 
				individuals associated with logistics, supply chains and the 
				transport industry. The decision to tie up with CILT was driven 
				by the opportunity created by the government’s thrust on 
				developing the ports and maritime sector. Initiatives like the 
				Sohar Port, Duqm Dry Dock, Port of Salalah, the conversion of 
				Port Sultan Qaboos into a full-fledged tourism port and the 
				coming up of a new Maritime College in Sohar convinced the 
				institute about the future potential of this sector. The classes 
				are expected to start by January 2012. Polyglot is also an 
				affiliate of the International House World Organisation (IHWO) – 
				a network of over 140 language schools worldwide. The institute 
				trains 5000 students annually.</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-cover05.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"><br>
				<br>
				National Training Institute (NTI) set up Sohar Training Centre (STC) 
				in 2007, opening doors for customers in the Al Batinah and 
				Buraimi regions. The STC provides technical training, HSE 
				training, BizPro products such as the business and language 
				skills and IT courses. The centre was established to meet the 
				needs of the burgeoning industries in Sohar. The STC has worked 
				with Sohar Aluminium to train its staff. The NTI’s HSE division 
				conducts health and safety courses, which are accredited by the 
				Institution of Occupational Safety and Health (IOSH). The 
				National Safety Council, USA similarly accredits all its driving 
				courses. The NTI-HSE has the distinction of being accepted as 
				the first Middle East Examination Centre to conduct the National 
				Examination Board in Occupational Safety and Health (NEBOSH) 
				courses. It is also the first training provider in the world to 
				be licensed by DuPont to deliver the Safety Training Observation 
				Programme (STOP).<br>
				<br>
				Khimji’s Training Institute (KTI) and Symbiosis Institute of 
				Business Management (SIBM), India signed a Memorandum of 
				Understanding in May 2009 to offer management training 
				programmes in the Sultanate. The KTI- SIBM offers management and 
				workforce development programmes, which include diploma and 
				post-graduate programmes in business management, marketing 
				management and human resource management. The KTI represents 
				NIIT, the global IT solutions corporation in Oman and is its 
				master-franchisee for the GCC region. Its other tie-ups include 
				the Institute of Sales and Marketing Management (ISMM UK), NCC 
				UK for English language courses, ACCA for accounting programmes 
				and so on. KTI has seen a CAGR of 8-12 per cent over the last 
				7-10 years.<br>
				<br>
				The training industry has two set of companies operating within 
				its purview – training institutes and training service 
				Institutes (TSI). Training institutes are recognised and graded 
				by the Ministry of Manpower based on various parameters. The 
				stipulations include a minimum number of classrooms, adequate 
				library facilities, qualification and experience of the 
				institute director, two faculty members for each discipline, 
				enough parking space et al. These institutes can organise 
				programmes which run from 40 to 200 hours. The TSIs operate on a 
				license given to them by the Ministry of Commerce and Industry. 
				They can run courses of not more than 24 hours and their 
				certificates are not stamped by the Ministry of Manpower, unlike 
				the training institutes.<br>
				<br>
				<img border="0" src="images/nov-cover06.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>In the breach<br>
				</b>Training institutes complain that TSIs flout the rules by 
				conducting more than 24-hour courses, issuing unauthorised 
				certificates and not maintaining proper standards or faculty. 
				The fact that their overheads are less compared to established 
				institutes allows them to offer courses cheaper. An Oracle 
				approved course from a grade-A training institute costs anywhere 
				between RO2,000-3,000 whereas the same is on offer in the grey 
				market at half the price. Says Lawrence Alva, CEO, NTI, “These 
				fly-by-night operators have killed the soft skills and business 
				training market.”<br>
				<br>
				The industry has also come to be plagued by low capacity 
				utilisation. The NTI established a technical vocational 
				institute in Ghala in 1990. The facility could train 400 people 
				at a time apart from providing them with residential facilities. 
				Unfortunately, as the numbers were not there the institute shut 
				down in 2005. It has a similar experience in Salalah where it 
				established an institute to help the Port of Salalah in 1998. 
				That too folded in 2001 due to non-availability of people.</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-cover07.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"><br>
				Others too have similar tales to share. The NHI trained 400 
				people in 2008, in 2011 these numbers were down to 150 or less 
				than 50 per cent of its capacity. The institute can train 
				400-500 people a year. Says MacLean, “The systems and procedures 
				being followed do not do justice to the opportunity in the 
				market.” Alva adds, “The mechanism is ineffective and does not 
				seem to be working.”<br>
				<br>
				Overall, the problem comes down to two issues – the tardy pace 
				of clearance from the Ministry of Manpower and an unwillingness 
				to foot the bill. If a company wants to recruit 20 people, it 
				approaches a training institute with the request. The institute 
				interviews people, checks their credentials and submits a list 
				to the Ministry of Manpower, under the NFP. The ministry takes 
				three to four months to issue a work order. The NHI submitted a 
				list of 120 people to the ministry in September 2011, but it is 
				expecting a decree to proceed only by December, and that too is 
				a best case scenario. Says Nadeem Ahmed, senior divisional 
				manager, Khimji Training Institute, “By the time the ministry 
				gives us a work order it goes into months, while it should not 
				take more than 15 days. There have been instances when a person 
				who has been unemployed for six months gets completely 
				frustrated by the time lag and uncertainty.”<br>
				<br>
				Footing the bill for training remains another big area of 
				concern for the industry. While a handful of big companies 
				realise the importance of training, there is an overall 
				reluctance to pay for training. Individuals are either not ready 
				to pay or do not have the ability to pay for training while the 
				government’s payment process is archaic and slow. All these 
				factors combine to create cash flow problems for training 
				institutes. Says Alva, “The culture to pay for training is not 
				there in Oman.”<br>
				<br>
				During the training period, the prospective employer pays a 
				stipend of RO100 and the training institute pays another RO50 to 
				the trainee. If an institute trains 100 people its stipend 
				expense comes to RO10,000. The government splits payments to 
				institutes in tranches of 20, 50 and 30 per cent. The training 
				institute is paid the last 30 per cent payment by the ministry 
				only after the trainee joins a company. If the trainee walks 
				away from the job before getting a labour contract, the training 
				institutes does not get paid the last tranche as the training is 
				considered to have been wasted. Training companies argue that 
				even though a person may not join a company, he or she uses his 
				skills in another job within the economy, so it would be unfair 
				to dub the entire exercise as a failure.<br>
				<br>
				The uncertainty of government support and funding has led 
				training companies to train their focus on the private sector. 
				The NHI introduced a new system called Fast Track training which 
				has been received well by the private sector. “We told companies 
				that you are recruiting untrained people, who tend to leave 
				within two months. Instead, give us two month’s salary of an 
				entry level employee and we will train them on attitude and 
				technical skills,” says MacLean. The idea has been received well 
				with companies like the Tawoos Group, Oman Catering, Al Sulaimi 
				Group and KFC. The success of this coupled with other 
				initiatives has a role reversal of sorts. In 2010, 65 per cent 
				of the NHI’s business came from the government, while in 2011, 
				the private sector’s contribution has moved upto 65 per cent 
				from 35 per cent in 2010. Polyglot gets 60 per cent of its 
				business from the private sector and 40 per cent from the 
				government. Says Ahmed, “There are companies which have 
				increased their training budgets from RO10,000 a couple of years 
				ago to RO100,000 now as they realise that long term growth has 
				to come from the national work force. Several companies are also 
				using training as an incentive or reward.” A number of people 
				who got employed in the wake of the Omanisation initiatives of 
				the government in the 1990s have reached supervisory levels and 
				they require training yet again. This necessitates private 
				sector spending on training.<br>
				<br>
				The government’s decision to pay a RO150 unemployment allowance 
				deters some youngsters from going in for training as they get 
				only a RO50 stipend during the course, the overwhelming feeling 
				being – ‘why should we lose RO100’. Says Al Qassim, “This is a 
				classic minimum wage trap; a number of times people feel that a 
				seven month course which gets them a RO200-250 salary is not 
				worth the effort. What they fail to realise is that to move up 
				the corporate ladder after a certain level requires 
				certification and skills.” Samar Al Sheikh, senior coach, Nawras 
				adds, “Satisfaction rather than pay should be the primary driver 
				of a job.”<br>
				<br>
				There was also a complaint that SMEs are not ready to pay for 
				training, but that too might be changing. The KTI organised a 
				one-day workshop with master trainer and author Ibrahim Elfiky 
				in April 2011. “Out of 300 people who attended the workshop by 
				paying RO150 each, 200 were from SMEs; a few of them nominated 
				as many as 10-15 people each,” says Ahmed.<br>
				<br>
				<img border="0" src="images/nov-cover08.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>A bigger canvas<br>
				</b>Big companies like Vale in Oman, BankMuscat, Nawras, Oman 
				LNG lay a lot of stress on training, mentoring and coaching. 
				Oman LNG has a social investment programme to which it earmarks 
				RO10mn every year. The gas major spends RO4mn-5mn on training 
				its staff per annum. Till date, Vale has invested more than 
				$11.3mn in specialised training and recruitment programmes for 
				engineers, technicians and operators to be employed in various 
				positions within the project. Says Marcos Beluco, Vale country 
				manager, “We believe that a thriving economy is highly dependent 
				on its human capital and therefore we have dedicated our efforts 
				to build the capacity of national talent specifically graduates 
				from the Batinah region through intensive training programmes.”<br>
				<br>
				Vale is committed to achieve an Omanisation rate of 80 per cent 
				by 2015 and it has enrolled more than 180 Omani students in its 
				two-year training and recruitment programme since 2009. In 
				October 2010, 65 students completed their intensive on-the-job 
				training in Brazil, while 17 students conducted their training 
				in Japan and 33 in Muscat. Vale also offers all its employees 
				additional educational courses through its educational arm, 
				Valer. The education programme in Oman offers English classes as 
				well as safety and technical courses.</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/nov-cover09.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				Oman LNG has trained 1,000 Omanis till date in various domains. 
				“We identify skill gaps in association with the Ministry of 
				Manpower and find companies who are willing to employ people 
				once they are trained and then train them at our cost,” says 
				Buckley. Oman LNG plans to train 200 Omanis in the next 12-16 
				months. It will also train another 200 Omanis in identified 
				skills without a prior job commitment with any company. The 
				company has a Learning Centre in Sur, where it does the initial 
				in-house training for its employees.<br>
				<br>
				<img border="0" src="images/nov-cover10.jpg" style="border: 1px solid #808080" class="style4" hspace="0" vspace="4" width="514" height="208"></font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">
				<b>Catching them young<br>
				</b>Most companies stress on a structured and in-depth induction 
				programme for new employees to inculcate its culture and 
				acquaint them with the requisite skills. Salim Al Kaabi, AGM – 
				HRM, BankMuscat says, “All our new staff members go through a 
				structured induction programme followed by an on the job 
				training in the branch/department. Additionally, they continue 
				to participate in other training modules based on the job that 
				they are required to do.”<br>
				<br>
				Nawras has an employee education programme as a part of which it 
				helps employees get diplomas, post-graduations or other degrees. 
				The company does not provide the entire fee but bears the 
				partial cost of education. It hired 150 Omanis in response to 
				His Majesty Sultan Qaboos bin Said’s call to create 50,000 jobs 
				in February 2011. Whenever Nawras advertises for jobs 
				opportunities it gets over thousands of applicants. While a 
				couple get the job, the company gives all of them an honest 
				feedback on their strengths and weaknesses. Says Samar, “Even if 
				we cannot give all of them a job we do give them something that 
				helps them in future.”<br>
				<br>
				Though a number of graduates come with a high CGPA or a good 
				degree, employers are on the look out for something extra. The 
				primary skills that most companies look for in young Omanis are 
				a good attitude, customer orientation, good communication 
				skills, and an ability to work in a team. Says Kumail, “When a 
				graduate comes in, the first thing that helps him is a positive 
				attitude. We put new recruits through an eight-week training 
				programme in which he is exposed to our culture, ethics, soft 
				skills and telecom training.”</font><p align="justify" style="margin: 10px">
				<font size="1" face="Verdana">A good induction programme serves 
				as a springboard for employees, but companies realise that 
				training is an ongoing rather than a one-off activity. Says 
				Salim, “We nominate our employees for programmes, both in Oman 
				and abroad, as and when needed. This is to learn the best 
				practices and network for business. We also work with various 
				partners from across the world, who design and deliver 
				customised programmes, both on the functional and behavioural 
				sides.” BankMuscat uses SHL group from UK, Moody’s, Euromoney, 
				DC Gardner, ASTD globally and local institutes like NTI, NHI, 
				Polyglot, Infoline and others to impart training to its 
				employees.<br>
				<br>
				In a world where there is a growing divide between the people 
				who have the right training, education, skills and the ones who 
				are deprived of it, young Omanis can overlook this factor but at 
				their own peril. Kumail sums up, “If companies or individuals 
				think that the cost of training is too steep then they should 
				ask themselves what the cost of ignorance is.” Do we need to say 
				more?</font><br>
