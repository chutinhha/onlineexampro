				Nawras, Canon – Genetco, Audi and Ominvest while the support 
				partners are Al Habib, Taageer Finance and Sharq Sohar Steel. 
				The media partners will be Times of Oman and Al Shabiba 
				newspapers while the official call centre is Spanco.<br>
