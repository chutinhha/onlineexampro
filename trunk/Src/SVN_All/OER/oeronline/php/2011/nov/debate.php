				of the most eagerly expected event in corporate Oman’s calendar 
				is the ‘Oman Debate’ 2011, organised by Oman Economic Review. 
				Thanks to the grassroots issues tackled in a healthy atmosphere 
				where representatives from both the public and private sectors 
				are present, a lot of excitement has been generated around the 
				third edition of the event.<br>
				<br>
				The date of the event has been rescheduled in order to get 
				better representation from key stakeholders from both the public 
				and private sectors.<br>
				<br>
				Renowned television host Tim Sebastian – the founder and 
				chairman of ‘The Doha Debates’ and former BBC Hard Talk 
				presenter – will anchor the Oman Debate 2011. The debate will 
				feature a distinguished panel of speakers to discuss core 
				issues.<br>
				<br>
				The annual national debate is organised by OER, the leading 
				business magazine in the Sultanate, in association with the 
				Capital Market Authority (CMA). The Oman Debate encompasses key 
				themes that form the business environment of today and tomorrow 
				in Oman. The hot topic of discussion for Oman Debate 2011 is how 
				to cope up with the developments of the Arab Spring, which has 
				had its repercussions on the Sultanate.<br>
				<br>
