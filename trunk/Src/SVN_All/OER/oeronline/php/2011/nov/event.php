						Making a difference</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana"><span class="style5"><strong><br>
				<span style="font-size: 9pt">The Al Mar’a Excellence Awards will 
				recognise and honour women across diverse fields<br>
				</span></strong></span>
				<br>
				</font><font face="Verdana" size="1">
				The countdown to the first ever Al Mar’a Excellence Awards has 
				begun. With nominations pouring in, the scintillating event will 
				now be held on December 5 at The Amphitheatre, Shangri-La’s Barr 
				Al Jissah Resort &amp; Spa.<br>
				<br>
				The event has been shifted to a new date owing to the large 
				number of nominations. Women are not only nominating themselves 
				but other women as well. The extended time period will 
				facilitate more women to be part of the awards process and offer 
				a wide selection of the most deserving winners. <br>
				<br>
				The awards, a first-of-its-kind initiative seeks to applaud, 
				honour and acknowledge women from different walks of life. <br>
				<br>
				The Al Mar’a Excellence Awards will honour women from the 
				following fields and profiles.<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for Corporate 
				Leadership<br>
				</b></font>She leverages her achievements and business acumen to 
				facilitate change and create opportunities. She demonstrates 
				respect for and engagement with her community, clients and 
				colleagues.<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for Fashion 
				Design<br>
				</b></font>The ultimate accolade, which recognises an acclaimed 
				designer who has made a significant impact in the nation and 
				abroad.<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for 
				Performing Arts<br>
				</b></font>She is an individual whose energy and excellence have 
				defined or redefined the art for today’s discerning audiences.
				<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for Fine Arts<br>
				</b></font>She is an individual whose energy and excellence have 
				defined or redefined the art forms of 
				painting/sculpting/photography. <br>
				<font color="#C00000"><b><br>
				Al Mar’a Excellence Award for Educational services<br>
				</b></font>She contributes through practice, research or 
				administration in a learning environment, including the 
				education system, employee training and community education. <br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for 
				Entrepreneurship and Innovation<br>
				</b></font>She has taken an innovative approach to solving a 
				problem, filling a need or breaking into a new market. She has 
				made sacrifices and risked a great deal to successfully manage, 
				launch or revive a business, product or service.<br>
				<font color="#C00000"><b><br>
				Al Mar’a Excellence Award for Health Services<br>
				</b></font>She is unwavering in her commitment to building 
				healthy communities by elevating the values of physical, mental 
				or spiritual well-being. <br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for 
				achievements in Science and Industry<br>
				</b></font>She is dedicated to the advancement of scientific or 
				industrial discovery or application. Her efforts forge new roles 
				for women and create opportunities for future generations in 
				fields of science, applied science and natural resources and 
				information technology.<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for 
				Technology<br>
				</b></font>She is a leader in technical know-how and is 
				committed to the advancement of technology. Her efforts forge 
				new roles for women and create opportunities for future 
				generations in the field of information technology.<br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for Sports<br>
				</b></font>She is an exceptional woman who has taken her country 
				to new heights of fame and success in the field of sports. <br>
				<br>
				<font color="#C00000"><b>Al Mar’a Excellence Award for Petroleum 
				Services<br>
				</b></font>She is a leader in the petroleum services industry 
				and is committed to the advancement of energy services. <br>
				<font color="#C00000"><b><br>
				Al Mar’a Excellence Award for Social Responsibility<br>
				</b></font>She is unwavering in her commitment to her 
				community/environment and to uplifting and empowering less 
				privileged/disabled people or threatened wildlife and 
				biodiversity. <br>
				<br>
				In addition, there will be awards for Woman of the Year and the 
				Most Promising Young Woman. The Awards will judge achievers 
				according to different parameters like innovation and 
				creativity, the impact their contributions have had in society, 
				their leadership abilities and the goals achieved. The final 
				ranking process with be appraised by our official assessment 
				partner, Ernst &amp; Young. <br>
				<br>
				How does the nomination process work? If you wish to nominate 
				yourself or other women who you think deserve to be honoured for 
				making a mark in their respective fields and thereby impacting 
				society, all you have to do is log onto www.almaraonline.com or 
				fill up the nomination forms also available at selected retail 
				outlets, which will then have to be submitted with your (or your 
				nominee’s) supporting documentation at United Media Services, 
				Ruwi.<br>
				<br>
				The Al Mar’a Excellence Awards 2011 is an Al Mar’a Initiative. 
				Strategic Partners: Mercedes-Benz (Zawawi Trading Company, the 
				authorised general distributor for Mercedes-Benz in Oman), 
				BankDhofar and Nawras. Support partner is Taageer Finance 
				Company SAOG. Assessment Partner: Ernst &amp; Young. Media Partners: 
				Times of Oman, Al Shabiba, H! and Al Youm Al Sabe. Radio 
				Partners: Al Wisal FM and Merge 104.8 FM. Printing Partner: Ruwi 
				Modern Printers.<br>
				<br>
