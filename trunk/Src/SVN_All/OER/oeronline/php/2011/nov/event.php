				organisations speak about what this partnership means to them …<br>
				</b> </font><font face="Verdana" size="1">
				<br>
				<b>Craig Hardie, CEO, Mercedes-Benz Oman<br>
				</b>By sponsoring this event, the Management of Zawawi Trading 
				Co, the authorised General Distributor of Mercedes-Benz in the 
				Sultanate, would like to convey a vital message that, in line 
				with the policies of His Majesty Sultan Qaboos bin Said, we 
				stand behind important social causes in the Omani society of 
				today, such as empowerment/support of Omani women of zeal and 
				ambition. The main slogan of Mercedes-Benz, “The Best or 
				Nothing”, applies not only to class and luxury, but reliability 
				and safety as well. As all the aspects of our brand are aligned 
				with excellence, it is our honour to support and recognise the 
				excellence achieved and expressed through the exceptional women 
				of the Sultanate. <br>
				<br>
				<br>
				<b>Anthony Mahoney, CEO, BankDhofar<br>
				</b>BankDhofar’s decision to partner with The Al Mar’a 
				Excellence Awards is an excellent way to show our support to the 
				women in Oman who deserve recognition for their accomplishments.<br>
				<br>
				<br>
				<b>Ross Cormack, CEO, Nawras<br>
				</b>His Majesty Sultan Qaboos bin Said has spoken of the 
				important role that women undertake in the country and Nawras is 
				committed to playing its part in recognising and encouraging 
				women’s contribution to society, be it at work, at home or in 
				the wider community. We are delighted to support this event to 
				recognise excellence as our own core values are to be caring, 
				excellent and pleasingly different.</font><p align="justify" style="margin: 3px 10px">
