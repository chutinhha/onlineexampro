				<b><br>
				A number of new tourism projects are on the anvil in the country 
				or have opened up recently. What are the opportunities for the 
				OUA in these areas?<br>
				</b>Chris Charters: We are genuinely very excited about the 
				upcoming developments and as a forward thinking company we 
				ensure that we are in touch with all the companies actively 
				involved or considering opening facilities in Oman. As part of a 
				much larger organisation we are able to draw on the 
				relationships we have with the hotel companies whether it’s 
				through our operations in the UAE, Africa or the Indian Ocean.<br>
				<br>
				Evert Boersma: We have been extremely successful so far with all 
				the recently opened projects and we have managed to secure the 
				majority share of the business. This is gained through market 
				knowledge, our experience and above all our ability to match the 
				expectations of our customers. New projects would also mean an 
				inflow of expats which would translate into an increase of our 
				customer base in the retail segment. <br>
				<br>
				<b>How does the OUA ensure that its brand visibility is high 
				considering that above the line advertising is not possible?<br>
				</b>CC: We do a lot of research each year through third party 
				companies as well as our own surveys to ensure we understand how 
				both the customer and the final consumer perceive our brands and 
				products. Having an experienced and dynamic marketing team, we 
				ensure that through participation in various events, within the 
				retail environment our key brands perception by consumers is 
				enhanced and reinforced on as many platforms as possible.<br>
				<br>
				EB: As the laws in Oman prohibit above the line advertising, it 
				is critical in our industry to gain customer satisfaction by 
				providing the best in store customer experience leading to 
				positive word of mouth references resulting in new customers.<br>
