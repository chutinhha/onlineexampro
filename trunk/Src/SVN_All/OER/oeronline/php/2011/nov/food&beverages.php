						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Pepping up the tourism industry</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana"><span class="style5"><strong><br>
				<span style="font-size: 9pt">OUA, one of the biggest beverage 
				companies in the country, has grown consistently over the past 
				three years. The company also did a couple of key acquisitions 
				to steadily grow its market share. Nilesh Samani, Group General 
				Manager; Chris Charters, Sales and Marketing Manager; Dushyanth 
				Narayanan, Senior Manager; and Evert Boersma, Country Manager, 
				Oman for Sirocco share their views about the company’s success 
				story<br>
				<br>
				</span></strong></span>
				<br>
				</font><font face="Verdana" size="1">
				<b>Your comments on the company’s performance and market share 
				in the past one year<br>
				</b>Nilesh Samani: Oman has witnessed a growth in tourism 
				consistently over the past 2-3 years and so has the OUA grown 
				over last three years. The year 2011 has been an eventful year 
				with the overall situation in the MENA region affecting our Q1 
				and Q2 performance to some extent. However, our overall market 
				share has grown and we expect a robust Q4 due to the approaching 
				festive season.<br>
				<br>
				<b>What about the growth of the subsidiary companies?<br>
				</b>NS: Being one of the biggest companies in the beverage 
				industry and having acquired market leadership, the OUA 
				strategically went on to acquire ONAS during 2003 and Sohar 
				Catering &amp; Supplies during 2008 to steadily grow OUA’s market 
				share and strengthen its dominant position within the industry.<br>
				<br>
				<b>How is the OUA contributing to the growth of the tourism 
				industry in Oman?<br>
				</b>Chris Charters: The OUA is the leading alcoholic beverage 
				distributor in Oman and we have an important role to play in 
				ensuring that tourists find internationally well-known products 
				and brands that are familiar to them back home. We spend 
				significant time in ensuring that we research the consumer 
				profile of the people visiting Oman and understanding their 
				wants and needs. We also invest heavily in imparting training to 
				our customers and also to our staff so that the end consumer 
				gets a fulfilling experience.<br>
				<br>
				<b>The OUA has expanded its top level team. What is the reason 
				behind this?<br>
				</b>NS: The market is growing and with the upcoming expected 
				growth in tourism the OUA needs to expand its organisational 
				strengths now so that we are well placed to manage the changes 
				in the structure of the industry as we move forward over the 
				next five years. We have brought in people with great experience 
				in our core business areas of retail and on-trade sales as well 
				as trade marketing. Above all, our recruitment policy ensures we 
				are bringing enthusiasm and drive to the team so that we can 
				service our customers better.<br>
				<br>
				<b>Most companies are into recruitment drive following the 
				directive of the government. What steps has the OUA taken in 
				this regard?<br>
				</b>NS: We are ensuring that we take as many local employees as 
				possible. Because of the nature of our business, it is sometimes 
				difficult to recruit local employees to work with us. However, 
				we are able to integrate and effectively employ local employees. 
				We have also tied up with local training institutions to recruit 
				young fresh Omanis and groom them into responsible positions.<br>
				<br>
				<b>What are the staff welfare measures that are in place in the 
				OUA and are you meeting the stipulated Omanisation rates?<br>
				</b>NS: The OUA believes and consider semployees as its most 
				important assets and accords high priority to their welfare. In 
				addition to providing staff with basic welfare in line with Oman 
				Labour Law, the OUA considers training and development of its 
				staff as top priority. The OUA budgets for its training needs 
				and provides the necessary training and development to its staff 
				to enhance their skills. The company also holds various staff 
				engagement events like Family Day, Sports Day which help the 
				staff members and their families to interact with each other and 
				seal new friendships. The OUA celebrates all festivals with 
				great vigour to bring out the festive spirit amongst all.<br>
				<br>
				<b>What are the strategies to grow your retail market share?<br>
				</b>Dushyanth Narayanan: Our strategy would be to open new shops 
				to provide easy access to our customers. We have the potential 
				to grow retail over the next few years if we can secure 
				locations approved by the ROP. In Muscat, there are areas where 
				a large number of expatriates work or reside and we are 
				analysing the prospects of opening retail outlets there; outside 
				Muscat, we are also investigating the potential for a number of 
				outlets in areas currently under development or where there are 
				no current retail facilities. It’s not a straightforward process 
				and as a responsible company we are very much aware of the 
				sensitivities involved and this is the first and foremost thing 
				in our minds as we look to extend our retail portfolio. The OUA 
				strives to provide the best in store shopping experience in Oman 
				in terms of store layout and environment, wide selection, 
				competitive pricing and knowledgeable retail staff. We engage 
				and take feedback from our customers to continuously improve our 
				services. </font>
