                    </font><font face="Verdana" size="4"><b>Crude Awakening <br>
                </b></font><font face="Verdana" size="1"><b>Mustafa Ahmed Salman, 
                CEO, United Securities</b> was recently asked by World Fi<img border="0" src="images/img73.jpg" align="right" hspace="8" vspace="6" width="400" height="268">nance 
                magazine to comment on investment options in the GCC. He speaks 
                to Mayank Singh about the opportunities amidst the crisis<br>
                <br>
                <b>In the cover story of World Finance magazine, you argue that 
                the ‘economic</b><b> expansion of the GCC countries is here to stay.’ 
                What makes you confident about this?</b><br>
                World Finance is a famous magazine in the world and when they 
                asked us to speak about Oman’s potential as an investment 
                destination, we felt that since foreign investors usually look 
                at the entire region it would be more relevant to talk about the 
                region as a whole. The financial crisis has had a minimal impact 
                on this part of the world compared to the other areas because of 
                a couple of factors. One, most of the GCC countries are 
                dependent on oil and oil prices have stablised. Second, most of 
                them have a surplus. In the next one year we believe that the 
                region will be the best market in the world to invest in. We 
                expect oil companies to invest heavily in the exploration and 
                production (E&amp;P) in the region, which augurs well for the 
                stockmarket. <br>
                <br>
                <b>What is your take on oil prices in future?</b><br>
                We expect oil prices to remain in the $60-$70 per barrel range. 
                We saw oil prices sharply moving from $50 per barrel to $70 per 
                barrel and now they are moving back to $60 per barrel. 
                Historically, oil prices have always followed a U shaped curve 
                and that holds true even today. The ministry of oil and gas in 
                Oman has signed a number of E&amp;P contracts, which shows that 
                investors are coming back to the region. A lot of work has been 
                done in Iraq in the last two years. We expect a number of public 
                companies in the GCC region to get into E&amp;P. A few companies in 
                Qatar and Kuwait have already started this. <br>
                <b><br>
                How do you see the MSM index moving in the coming months?</b><br>
                The markets will move after Qtr2 results of companies are 
                announced as investors are looking for some clarity. The global 
                liquidity crisis has had some impact on the banking sector, 
                which has made investors wary of investing in banks, but apart 
                from this there has been a minimal impact of the crisis. <br>
                Do you expect emerging markets to outperform stock markets from 
                developed countries? <br>
                Emerging markets have historically led economic booms. Investors 
                can focus on Asian markets, Malaysian markets and the GCC. The 
                GCC markets which witnessed a heavy fall in 2008 have already 
                recovered in 2009, which promises good return for investors. <br>
                <b><br>
                What kind of a strategy should retail investors follow while 
                investing in the stockmarket? </b><br>
                Earlier most investments on the market was by big groups. But 
                lately, we have seen the entry of smaller investors on the MSM, 
                with most of them investing through mutual funds. Though I feel 
                that direct investments give better results. Investors should 
                base their decisions on fundamentals rather than on technical 
                speculation. <br>
                <b><br>
                Are there any sector picks that you would like to suggest? </b>
                <br>
                People at times worry about the industrial sector, but I believe 
                that the industrial sector may be better than other sectors. The 
                government has not slowed down infrastructure spending and those 
                companies whose fortunes are linked to such projects will 
