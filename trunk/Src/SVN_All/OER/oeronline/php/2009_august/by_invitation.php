		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">A MUTUALLY 
                BENEFICIAL RELATIONSHIP</font></b><font face="Verdana" size="1"><b><font face="Verdana" size="2" color="#CC3300">&nbsp;&nbsp;
                </font>
                <img src="images/img31.jpg" hspace="9" vspace="1" align="right"><font face="Verdana" size="2" color="#CC3300"><br>
                </font>There are abundant opportunities for future cooperation 
                between india and oman in a number of sectors and both indian 
                and omani companies need to seize these opportunities<font face="Verdana" size="2" color="#CC3300"><br>
                <br>
                </font></b>India and Oman have been friendly neighbours sharing 
                a vibrant legacy of trade, partnership, 
                kinship and friendship spanning thousands of years and the 
                cultural cross fertilisation that took place through 
                people-to-people contacts has been mutually enriching. These 
                traditionally close and friendly ties have been consistently 
                developed in all fields and today stand on firm foundations with 
                splendid prospects of the cooperation attaining its full 
                potential. <br>
                <br>
                Our economic and commercial relations are on the path of rapid 
                ascendancy. In 2008, our non-oil bilateral trade figures stood 
                at $1.67bn. Over one hundred and thirty Indian companies have a 
                foothold in Oman in different sectors such as construction, 
                manufacturing, trading, telecommunications, oil and gas, 
                fertiliser, education, banking and finance, insurance etc. 
                Similarly, over thirty Omani companies are present in India in 
                such sectors as oil and gas, IT, banking and finance, 
                pharmaceuticals, manufacturing etc. Bilateral investment 
                relation between India and Oman is also quite strong and is 
                increasing steadily. <br>
                <br>
                <b>Diplomatic exchange</b><br>
                Several high-level visits that have taken place in recent past, 
                including the visit of His Highness Sayyid Fahd Bin Mahmoud Al 
                Said to India in December 2007 and the visit of Hon’ble Prime 
                Minister Dr Manmohan Singh to Oman in November 2008 have 
                provided further impetus to these relations. One significant 
                development was the decision to establish an India-Oman Joint 
                Investment Fund with an initial seed capital of $100mn and an 
                authorised capital of $1.5bn. A lot of progress has been 
                achieved during several rounds of technical level talks between 
                State Bank of India and the State General Reserve Fund and we 
                expect that the detailed joint venture agreement (JVA) will be 
                signed in the very near future.<br>
                <br>
                Another key outcome of the Indian Prime Minister’s visit was 
                that a high-level India-Oman Working Group was established under 
                the co-chairmanship of His Excellency Dr Mohammed Al Zu<img border="0" src="images/img03.jpg" align="right" hspace="8" vspace="4" width="300" height="266">bair Bin 
                Ali, Advisor to His Majesty Sultan Qaboos Bin Said on economic 
                planning affairs and Dr Montek Singh Ahluwalia, deputy chairman, 
                Planning Commission of India. Mandated with laying down a 
                roadmap for deeper and stronger economic engagement between the 
                two countries, the India-Oman Working Group has met twice and 
                identified a number of sectors for future cooperation, which 
                include food security, healthcare, tourism, education and 
                training among others.<br>
                <br>
                There are significant collaborations that exist at the moment 
                between Indian and Omani institutions of higher learning. Oman &amp; 
                Emirates Investment Holding Company and Central Institute of 
                Plastics Engineering and Technology (CIPET) are setting up an 
                Omani College of Plastics Engineering Technology in Sohar. 
                Sultan Qaboos University has research collaborations with a 
                number of universities/ colleges/ institutes in India, such as 
                CMC, Vellore, National Institute of Oceanography, Goa etc. 
                Institute of Chartered Accounts of India (ICAI), New Delhi and 
                College of Banking and Financial Studies (CBFS), Muscat have 
                signed an MOU for introducing accountancy courses in Oman. The 
                Omzest Group has commenced the process of setting up an 
                Institute of Cost and Management Accountants in Oman, in 
                association with The Institute of Cost and Works Accountants of 
                India (ICWAI), Hyderabad. This institute will work towards 
                promoting and propagating the profession of cost and management 
                accountancy in Oman.<br>
                <br>
                Khimji Training Institute has also signed an MOU with Manipal 
                University for setting up a higher education centre for various 
                disciplines. Most recently, it has signed an MOU with a premier 
                management institute from India, i.e. Pune-based Symbiosis for 
                offering management courses in Oman. India and Oman have set up 
                a joint working group on higher education under the MOU on 
                cooperation in higher education that was signed during the visit 
                of His Highness Sayyid Fahd Bin Mahmoud Al Said, Omani Deputy 
                Prime Minister for the council of ministers in December 2007. 
                The first meeting of the JWG took place in Muscat in the last 
                week of March 2009 has identified a number of areas for future 
                cooperation. These include students exchange programme, 
                secondment of Indian lecturers to Omani colleges, etc. Oman is 
                also seeking Indian assistance in R&amp;D and curriculum audit in 
                its Colleges of Applied Sciences. All these initiatives would 
                further bolster the already strong India-Oman relations, 
                especially in the field of cooperation in higher education. <br>
                <br>
                With a view to enhance dissemination of information, we have 
                instituted a journalists’ exchange programme, under which there 
                would be regular exchange visits of Indian and Omani 
                journalists. We expect the finalisation of an MOU on cultural 
                cooperation to happen in the near future which will further 
                enhance exchange of students, academicians and cultural troupes 
                between the two countries.<br>
                <br>
                <b>A bright future</b><br>
                The future holds a lot of promise for further deepening of ties 
                between India and Oman. Indian economy has been witnessing a 
                phenomenal growth since the last decade. The country is still 
                holding its ground in 
                the midst of the current global financial crisis. In fact, when 
                most economies are registering negative growth, Indian economy 
                has grown at 5.8 per cent in the fourth quarter of 2008, a 
                marginal yet encouraging improvement over 5.3 per cent growth in 
                the third quarter. About 7,500 international business people 
                from over 24 nations polled in a Business Confidence Survey by 
                Servcorp International in April 2009, ranked India as the third 
                most attractive business destination even in these turbulent 
                times. The Indian bourse is performing well and the key market 
                indices are on an upward curve reflecting positive market 
                sentiments. Similarly, propped by prudent and conservative 
                government policies, the Omani economy has not shown any symptom 
                of slowing down. Global oil prices have started recovering after 
                a big slump and with current oil prices at over US$65 a barrel, 
                Oman is all set to march with its massive developmental plans 
                with more comfort and confidence.<br>
                <br>
                There are a number of areas where Oman and India can work 
                together for mutual economic benefits. The domains with large 
                prospects of cooperation include oil &amp; gas, infrastructure, IT, 
                education &amp; training, healthcare, tourism and hospitality, new &amp; 
                renewable energy etc. <br>
                <br>
                In keeping with its strategy of opening up oil &amp; gas exploration 
                sector to international firms, the Omani government has in 2009 
                alone awarded four blocks to MNCs. The Ministry of Oil &amp; Gas has 
                announc<img border="0" src="images/img05.jpg" align="right" hspace="8" vspace="5" width="300" height="265">ed that it intends to award some more blocks in the near 
                future. Though Indian companies at present have three 
                concessions in Oman, we will be glad to see some more 
                concessions being taken up for exploration by Indian companies 
                either individually or as consortia.<br>
                <br>
                India has been sourcing some LNG from Oman through spot 
                purchases. Rapid industrial growth in India is creating 
                burgeoning demands for LNG and we will be keen to see more LNG 
                exports from Oman to India. A global consortium spearheaded by 
                South Asia Gas Enterprise is looking at the possibility of 
                laying down a deep sea pipeline between India and Oman provided 
                sourcing of gas from the region can be firmed up. We will be 
                interested to see if India and Oman can join hands to develop 
                some gas blocks in third countries in the region. Furthermore, 
                the wholly-government owned Oman Oil Company, which also happens 
                to be the main investment arm of the government can look into 
                investment options in refineries in India. <br>
                <br>
                In the field of IT, Knowledge Oasis Muscat is planning its 
                fourth phase expansion which provides very good opportunities 
                for IT companies to set base in Oman that can service the Middle 
                East and North Africa region. We have a big challenge in hand to 
                convince Indian IT and IT-enabled services companies to 
                seriously look at these opportunities. <br>
                <br>
                Healthcare is another such sector that holds significant 
                promise. Considering the advantages that such complementarities 
                can offer, we are examining the possibility of joint development 
                of a super-specialty hospital in India with a reha<b><img border="0" src="images/img32.jpg" align="left" hspace="7" vspace="5" width="175" height="448"></b>bilitation 
                center in Oman that can complement each other. This will boost 
                medical tourism in both countries.<br>
                <br>
                India and Oman can both immensely benefit from investment 
                avenues created by Omani government to develop the country’s 
                tourism infrastructure on a war footing. Indian hotel groups and 
                hotel management training institutes should look at these 
                avenues with seriousness. The establishment of The Research 
                Council and a special research fund by the Sultan Qaboos 
                University has given a strong fillip to R&amp;D in the Sultanate. 
                India is fast emerging as a global R&amp;D hub in biotechnology, 
                pharmaceuticals, semiconductors etc. There are lots of synergies 
                that can be harnessed in this area. <br>
                <br>
                Driven by the necessity of upgrading its infrastructure to 
                sustain its economic growth at the current level and to 
                accelerate it further, the Indian government is undertaking 
                regulatory reforms with alacrity and encouraging public private 
                partnerships and FDI in core infrastructure sectors. Several 
                Omani companies have developed competencies in construction and 
                infrastructure and can benefit from participation in the 
                business opportunities provided by India.<br>
                <br>
                Similarly, Indian giants like L&amp;T, GMR have been involved both 
                domestically in development of state-of-the-art airports and can 
                play active roles in airport/ground infrastructure development 
                in the Sultanate. The Sultanate has prudently decided to develop 
                a national railway network that will ultimately connect all the 
                four major ports of the Sultanate, i.e. the existing ports at 
                Muscat, Sohar and Salalah and Duqm, currently under development. 
                The railway network will also be integrated to the GCC railway 
                grid in future. Indian Railway Construction Company (IRCON) has 
                unmatched expertise in EPC in railway sector. Apart from being 
                responsible for construction, operation and maintenance of the 
                world’s second largest railway network, it has also executed 
                projects in Sri Lanka, Algeria, Malaysia, Iraq and elsewhere. IRCON will be glad to offer its expertise in developing the 
                railway network in Oman.<br>
                <br>
                Another area where India has a lot of strength is new &amp; 
                renewable energy. The Omani government is planning to set up 
                small renewable energy pilot projects in a few locations and 
                also intends to build a large scale solar power plant of 50 
                MW-200 MW capacity. Many Indian companies like Tata BP Solar, 
                Acme Group, Moser Baer Photovoltaic are leading players in solar 
                power sector in India. Similarly, in the wind energy sector, 
                Suzlon and Sri Ganesh Windpower Engineers Private Ltd have 
                already shown interest in participating in wind power projects 
                in Oman.<br>
                <br>
                The Omani government has announced plans to set up a 
                <img border="0" src="images/img35.jpg" align="right" hspace="6" vspace="5" width="400" height="263">coal based 
                power plant and according to reports published in the press, the 
                Omani authorities have decided on super critical and subcritical 
                coal-based thermal power plant technologies. NTPC Ltd, a public 
                sector enterprise which is also the largest power producer in 
                India, has developed expertise in these technologies and 
                pioneers industrial usage of fly ash, a byproduct of the 
                coal-based power stations that can substantially reduce 
                pollution worries by using it for bricks and cement 
                manufacturing. <br>
                <br>
                To sum up, there are abundant opportunities for future 
                cooperation between India and Oman in a number of sectors. Both 
                Indian and Omani companies need to seize these opportunities. 
                This, I think, holds the key to the translation of the vision of 
                both leaderships in India and Oman, i.e. to transform the 
                bilateral engagement into a “strategic” one through “creation of 
                mutual economic stakes.”<br>
