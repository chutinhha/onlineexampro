                    <br>
                    <b>Malcolm Xavier Crasta drives the latest 750Li to find out</b><br>
                    The BMW 7-series was first introduced in 1977 in the form of 
                the E23. It was a repl<img border="0" src="images/img44.jpg" align="right" hspace="8" vspace="3" width="299" height="230">acement for the E3 large sedan and was in 
                production until 1987, when it was replaced by the E32. With 
                each successive generation the popularity of the model grew and 
                culminated with the release of the E38 in 1994. This particular 
                model was the epitome of life of the 7-series and was by far one 
                of the best. In fact when it was due to be replaced by its 
                successor, the E65 in 2002, its value only grew. This was 
                primarily because of two reasons. Firstly, it was an extremely 
                popular car and people wanted to grab them while they lasted and 
                secondly, its successor was not very well received by the 
                masses. Despite it being a technological masterpiece, the 
                primary reason for this lack of initial demand was the E65’s 
                radical, yet controversial, styling and also the functionality 
                and software issues that accompanied the iDrive. Despite these 
                facts, the E65 range (E65, E66, E67, E68) broke all records and 
                went on to be the best selling 7-series of all time, especially 
                after the 2006 facelift. BMW has now released the latest 
                iteration of the 7-series and a new chapter in its history, the 
                F01. The new model is all set to be the best ever in the 
                illustrious, albeit somewhat controversial, history of the model 
                range and with good reason. <br>
                <br>
                The new car is definitely a looker. Although it bears a strong 
                resemblance to its predecessor, it is amazing how refreshingly 
                different it looks. It’s thoroughly modern and progressive 
                design can truly hold its own in comparison to its rivals and we 
                are glad to note that BMW has gotten rid of the controversial 
                boot lid. Its handsome stance will surely garner attention and 
                will be perfectly at home parked at your local five-star hotel 
                or golf club.<br>
                <img src="images/img85.jpg" width="200" height="133" hspace="8" vspace="15" border="0" align="left"><br>
                The same carries over to the interior. High-quality materials 
                abound and the ambience is exclusive, sophisticated and above 
                all extremely inviting, So much so in fact, that you will 
                actually not want to leave. It gives you a feeling of luxury and 
                class before you even enter the vehicle and once you sink into 
                the luxurious, soft leather seats you feel perfectly at home. 
                The leg room is more than generous both in the front and rear 
                and there are more than enough electronic gizmos to make you 
                feel like you are in the Business Class seat of an airliner. <br>
                <br>
                Once in the driver’s seat, it is clear the sheer amount of customisability that is at your disposal. Everything can be 
                adjusted to suit anyone of any build with room to spare. While 
                we can go on about how good the materials feel or how well 
                designed the interior is; it is pointless because it is plainly 
                obvious that a lot of work has gone into designing it. Instead, 
                we will talk about the star feature – the iDrive. The completely 
                revamped system has a new interface, a new look and is now 
                completely user friendly. Everything now comes as second nature 
                and you never have to go past more than three menus. One look at 
                its simplicity and you wonder why BMW didn’t do this the first 
                time round. Aside from the iDrive there are a multitude of 
                features to quench your thirst for technology. You have a 
                rear-seat entertainment package with two adjustable monitors and 
                a DVD player, a head-up display, night vision, a side view 
                camera system and adaptive cruise control to name a few of the 
                features available. <br>
                <br>
                On the road the effortless performance of the 4.4-litre 
                powerhouse under the bonnet is immediately appar<img border="0" src="images/img84.jpg" align="right" vspace="5" hspace="8">ent. Although 
                the vehicle weighs in at over two tonnes the 400hp and 600Nm of 
                torque more than compensate and as a result the car hits 100kmph 
                in a mere 5.2 seconds, basically hi-performance sports car 
                territory. But the same cannot be said about the ride. While it 
                is still good by any standards, it is not quite as good as its 
                predecessor despite a dynamic damping control system. But this 
                is by far the only area in which it lags behind its predecessor.<br>
                <br>
                The rest of the car is easily up there with the best. The 
                refinement is top-notch, it is loaded to the brim with features, 
                it is one of the safest cars on the road today and to top it off 
                BMW’s have a respectable reliability record. So, if you are in 
                the market for a top-class luxury sedan, this is definitely one 
                car you should not pass up.</font></p>
