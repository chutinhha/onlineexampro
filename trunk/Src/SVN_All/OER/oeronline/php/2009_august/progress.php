                    <br>
                    HE Ahmed bin Abdulnabi Macki, Minister of National Economy 
                    and Deputy Chairman of Financial Affairs and Energy Resources 
                    Council unveiled Progress, an annual publication from United 
                    Media Services (UMS), celebrating the 39th Renaissance Day. 
                    Speaking on the occasion HE Macki said, “The development model 
                    followed by the 
                    Sultanate has ensured all round development and a stable economy.” 
                    <br>
                    <br>
                    This year’s edition stands out for its exhaustive coverage 
                    of various sectors like Oil and Gas, Banking and Finance, 
                    Capital Markets, Education and Healthcare, Insurance, Agriculture 
                    and Poultry, Retailing, Aviation, Infor<img border="0" src="images/img589.jpg" align="right" hspace="8" vspace="6">mation Technology, 
                    Real estate, Telecom, Manufacturing and Logistics, Power, 
                    Infrastructure, Tourism and Trade. Apart from top draw articles, 
                    this edition of Progress also has contributory pieces from 
                    a number of ministers. HE Dr Ali bin Mohammed bin Moosa, Minister 
                    of Health says, “The Sultanate’s world-leading position in 
                    public healthcare, as affirmed by the WHO, is the outcome 
                    of the visionary approach of the Ministry of Health in its 
                    strategic prioritisation of primary healthcare, combined with 
                    its structured planning activities undertaken through a succession 
                    of Five Year Plans initiated in 1970.”<br>
                    <br>
                    Says HE Maqbool bin Ali Sultan , Minister of Commerce and 
                    Industry, “The size of foreign trade and direct foreign investments 
                    have witnessed massive developments during the blessed Renaissance.” 
                    HE Dr Rajiha bint Abdulamir bin Ali, Minister of Tourism avers, 
                    “Since 1995, tourism has seen a massive development in terms 
                    of laying down its regulatory and organisational framework 
                    or developing the tourist sites at the different locations 
                    all over the Sultanate.” Sheikh Mohammed bin Ali al Qatabi 
                    – Minister of Fisheries adds, “The integration among fishing 
                    and other constituents of the Omani economy reflect the economic 
                    growth witnessed by the Sultanate in the last few decades.” 
                    HE Dr Rawiyah bint Saud Al Busaidiyah, Minister of Higher 
                    education echoes the views, “The government has provided a 
                    sound regulatory framework to ensure that the education process 
                    is up to world class standards.” <br>
                    <br>
                    Progress has seen an overwhelming participation of the corporate 
                    sector. The book gives companies a chance to showcase their 
                    achievements and to felicitate His Majesty Sultanate Qaboos 
                    bin Said on this momentous occasion.<br>
                    <br>
                    The 2009 edition with its variety of content and pleasing 
                    layouts is sure to appeal to a cross section of readers. The 
                    book can be found at bookstores across the Sultanate and is 
                    sure to adorn the bookshelves of readers <br>
                    for posterity.<br>
