                  <td width="100%"><img border="0" src="../../images/2000_september_october/energy.jpg" align="left" hspace="5" vspace="5" width="200" height="132"><font face="Verdana" size="4" color="#FF0000">Investing in Energy</font>
                    <p><font face="Verdana" size="1"><b>Despite coming on stream and already feeding large revenues into the local economy, the official inauguration of Oman LNG signifies another huge milestone for the coalition of government and overseas investors. OER looks at what more the oil and gas sector has to offer the Sultanate and how well the country is equipped to attract further investment. Nayl D’Souza reports</b></font></p>
                    <p><font face="Verdana" size="2">Despite global competition, Oman’s economy is, according to analysts, successfully charting a progressive course. As oil prices steam upward of $US30 per barrel mark, the Sultanate appears to be well placed to make the most of an investor-friendly climate. But is it adequate enough to meet the many challenges ahead? And if and when external factors change, will the government remain steadfast to the commitment made by international investors? Plus, what allowance is being made for the uncertainity of international oil prices?<br>
                    <br>
                    The key to a successful relationship between government and the private companies is to consider them as partners in achieving a common goal and to allow for a fair distribution of rent. External conditions are constantly changing and one finds that oil producing countries are willing to be flexible and adjust terms and conditions when needed to keep companies committed to their efforts, says Dr Herman Franssen, director of Petroleum Economics Ltd. and senior advisor to the Minister of Petroleum and Minerals from 1985-1996. For example, whenever oil prices reached very low levels, the Ministry of Oil and Gas (MOG) and the private partners in Petrol Development of Oman (PDO) would adjust the terms of their agreement to provide more government income. Following a significant recovery in the oil market, the private partners would recuperate some of the losses previously incurred. MOG’s relationship with its private partners in PDO has always been a relationship of give and take, keeping both sides sufficiently happy to continue.<br>
                    <br>
                    Dr Franssen’s observations are particularly poignant when considering the large projects abandoned in the stages of final negotiation such as the construction of a $US910 million petrochemical plant at Sohar which BP Amoco withdrew from. Others, like the joint-venture project between the Indian and Omani governments to set up a $US1.1 billion urea and ammonia plant close to the Oman LNG plant in Qalhat, have encountered acute delays before reaching final approval. Although the PDO relationship is unique, I believe it is still among the best agreements in the business. Oman’s government continues to be in control of the country’s resources and the private partners have been adequately compensated for the risks they take. The private partners have brought their very best geologists and engineers to Oman, using the latest E&amp;P technologies to find and develop oil and gas in one of the geologically most complex petroleum provinces in the Middle East. Without this long lasting relationship, one may doubt if Oman could have ever reached the current level of oil and gas production it has, he adds.<br>
                    <br>
                    Apart from relying on international borrowings supplied to the banks, the government requires co-sponsors to support projects such as the petrochemical, aluminium and fertiliser plants, says Mujib Moosa, financial analyst at Al Mawarid Securities. Of those willing to make a firm commitment, however, some have decided to suspend taking the decision to make the first step financially if not pull out altogether.<br>
                    <br>
                    While the Sultanate continues to pursue diversification of its economy to nurture industrial activity, to develop infrastructure, and to maximise the potential for growth, most analysts agree that the country’s policies of liberalisation and privatisation - major ingredients of the Sultanate’s Sixth Five-year Plan - are already beginning to pay dividends.<br>
                    <br>
                    The RO26.5 million first phase of the RO40.118 million Masarrat water project is underway and Societé Egyptienne D’Enterprises (SEDE) aim to complete before March 2002. At the time of OER going to press, Oman Gas Company (OGC) awarded contracts worth $US300 million for the construction of gas pipelines from Saih Nihayda to Sohar and Salalah, although the overall project cost is expected to top $US450 million.<br>
                    <br>
                    Indian company Dodsal has been chosen to construct the 700km gas pipeline from Saih Nihayda to Salalah at a cost of $US180 million to OGC while Italy’s Saipem and Snamprognetti with Greece’s Consolidated Contractor’s Company and Japan’s Mitsubishi have been awarded the 305km pipeline from Saig Nihayda to Sohar costing $US120 million.<br>
                    <br>
                    The Salalah gas pipeline will feed a gas-fired power plant in Raysut and increase the flow of energy to the Free Trade Zone at Salalah Port while the Sohar gas pipeline will serve the new Sohar industrial and port area. A further side agreeement on operation and maintenance to the existing gas transportation system was signed between OGC and Petroleum Development of Oman.<br>
                    <br>
                    Clearly, overseas companies that have already established a strong presence in the Sultanate are playing a part in driving further investment participation. The Shell Group and TotalElfFina are good examples. Korean giant Hyundai Engineering and Construction Co. is another. Recently, Hyundai won the engineering, procurement and construction (EPC) of the $US95 million Al Kamil power plant in Rimal Al Sharqiya, which is set to begin operations in 2002. Previously, Hyundai had constructed the PDO’s $US37.25 million Mina al Fahal refinery complex in 1982 and PDO’s $US10.1 million Nimr/Amal oil gathering station in 1985. It is currently involved in the RO9.8 million dredging and reclamation operation at Sohar Port.&nbsp;<br>
                    <br>
                    A notable newcomer to Oman is UK-based National Power, one of the world’s leading private power companies. It has won a 15-year concession to develop the Independent Power Plant at Al Kamil on a build, operate and transfer (BOT) basis and has hinted its interest in other projects, such as completing the Barka gas-based power and desalination facility by 2003.&nbsp;<br>
                    <br>
                    Meanwhile, Oman will soon spin off part of the telecommunications sector. Plans to privatize Oman Telecommunications Company (OmanTel) are in the final stages as Merrill Lynch and KPMG advise the government on how to go about the local floatation. OmanTel currently has 140,312 GSM subscribers and has recently expanded its capacity through a RO20 million expansion project with Ericsson, Motorola and Siemens to accommodate 222,000 users. The company also is keen to up Internet subscriptions, a number which currently hovers around the 20,000 mark.&nbsp;<br>
                    <br>
                    It is hoped that a strong information technology (IT) industry will not only help diversify the economy, attract foreign direct investment, and retain Omani capital in the country through its stock market (see OER July/August, 2000), but also provide employment.&nbsp;<br>
                    <br>
                    Tech companies are keen to expand their operations into the Sultanate because tight legislation relating to intellectual property rights already exists and is being improved. Microsoft works very closely with a huge number of partners in many technological and business areas. Having a presence in Oman would encourage these partners to invest in the local market, says Yasser El Din, Oman’s business development manager for Microsoft Gulf and Eastern Mediterranean (GEM). It is no secret that the all IT companies invest in countries which offer sound protection and that has always been a key consideration for all companies.</font></p>
