                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Oman's
                    Steely Future</font><p><font face="Verdana" size="1"><b>The construction of Oman's first steel billeting plant at Rusayl could be the beginning of a flourishing steel industry. Industry analysts argue that if the Sultanate can take advantage of its energy resources, solid infrastructure and proximity to export markets, the country could become a major steel producer in the region. OER reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2000_september_october/steely_future.jpg" align="left" hspace="5" vspace="5" width="200" height="143"></font><font face="Verdana" size="2">Oman's first steel billeting plant is shaping up at the Rusayl Industrial Estate, just outside Muscat. When it comes on stream later this year, many expect this unit to be at the vanguard of a promising steel industry taking root in the Sultanate. Although small in size compared to the steel-making behemoths of Asia and North America, the billeting plant of Modern Steel Mills LLC is being nevertheless touted as one of the 'heaviest' units to be established at this industrial hub. Housed within a cavernous industrial shed, it will also consume a hefty 15 megawatts (MW) of electricity per day, making it the most energy intensive plant on the estate.<br>
                    <br>
                    The project, involving a total investment of RO4 million, is being backed by a group of prominent Omani business houses and individuals. They include Sheikh Hamad Bin Hamoud Al Ghafri (Hamdan Trading Group), Ominvest, Hamad Hamed Al Ghafri (Future Group), Assarain Enterprises, Ashwin Dharamsey, Sheikh Salim Saeed Hamed Al Fannah Al Araimi and Mohammed Rashid Al Fannah Al Araimi (Galfar), Al Mamoorah Company, and Gulf Transport Co. LLC. The Industrial Bank of Oman (IOB) is pitching in with a commercial loan of RO2.05 million.<br>
                    <br>
                    Annual production, at 76,000 tonnes of billeted steel, may be modest by world standards, but the plant's significance to a potential steel industry in the Sultanate cannot be underestimated, says project consultant Hari Kumar Sreedharan."This project underscores the potential for a vibrant steel industry in this country, especially as Oman embarks on a path of economic growth powered by industrialisation and economic diversification," says Sreedharan, who is the general manager of Future Management Economic Consultancy (FMEC). FMEC is a part of the Future Group, which is a shareholder in the steel billeting project.<br>
                    <br>
                    He further adds: "Economic development will drive construction activity on a major scale, which in turn will require enormous quantities of steel in the form of sheets for industrial sheds, seamless pipes and flanges for the oil and gas sector, and rolled products for building construction. Clearly, there is a huge domestic market for steel products, not to mention soaring demand in the region, as well as the emerging markets of South Asia."Ashok Pavate, managing director of Semac Private Ltd, which are the engineering consultants for the project, agrees that a local steel industry is an eminently attractive proposition.&nbsp;<br>
                    <br>
                    "A steel plant can be a sure shot given the country's ideal location, favourable political and economic conditions, cheap energy and relatively easy access to finance. Also, the Sultanate's excellent relations with potential importing countries are an important determining factor." According to Pavate, plans for a major steel plant in the Sultanate were first discussed in the early 1980s. At this time, Indian engineering consultants MN Dastoor &amp; Co., a company credited with helping establish some of India's biggest steel plants, had strongly backed the idea of a steel mill in Oman. Nothing came of the proposal, however, largely because of the fact that at the time discoveries of natural gas vital for such energy-intensive industries was not considered abundant enough.&nbsp;<br>
                    <br>
                    Annual steel consumption in the Sultanate is projected to rise to 810,000 metric tonnes by 2005, further climbing to 1,100,000 tonnes by 2010. In contrast, demand in 1996 was pegged at around 600,000 tonnes, with steel bars and rods making up roughly 60% of all steel imports. Steel pipes accounted for about 30% of these imports. In fact, it is this insatiable appetite for steel that prompted the Japanese steel-making giant Kobe Steel Ltd, or KOBELCO, to forge plans for a world-class steel mill to be established at Sohar. Envisaged under proposals drawn up more than two years ago is a world-class direct reduction iron (DRI) complex of 1.2 million tonnes capacity rivalling some of the world's largest steel plants.&nbsp;<br>
                    <br>
                    A feasibility study conducted by the Japan International Cooperation Agency (JICA), a Japanese government-funded body, has priced the venture at $US784 million, with much of this investment proposed to be raised through term loans from Omani, Gulf and Japanese financial institutions. The JICA study has also recommended private equity capital from the Omani and Japanese promoters of the project.&nbsp;<br>
                    <br>
                    Efforts to get it off the ground are currently mired in complex negotiations with the government over supply of natural gas and electricity, which the promoters have sought at competitive rates. The energy-intensive plant will require about 0.8 trillion cubic feet (TCF) of natural gas over the 25-year life of the project, in addition to about 200 MW of power. Both sides are quietly optimistic that the project will eventually materialise, given the enormous benefits that will accrue to the Sultanate's economy. According to the JICA study, the venture will result in foreign currency savings to the tune of $US3.2 billion over 20 years, as imports of steel rods become a thing of the past. Moreover, the project's contribution to Oman's gross national product (GNP) will be significant as well - adding an estimated $US2.6 billion to the GNP over 20 years.<br>
                    <br>
                    KOBELCO not only plans to participate in the project equity, but assist in the management, training, and marketing as well. The bulk of steel manufactured by the plant is proposed to be exported elsewhere around the Gulf, and even beyond to markets in Asia and Africa. But unlike the DRI complex, which will depend on iron oxide pellets as raw material, Modern Steel Mills' steel billeting plant at Rusayl will turn to the huge scrap iron market for its raw material needs.&nbsp;<br>
                    <br>
                    According to Sreedharan, FMEC's Gulf and Middle East regions are awash in scrap iron and scrap military ordnance, thus serving as an inexhaustible source of raw material. The Sultanate is also a major producer of scrap iron, with exports in the order of 60,000 metric tonnes per annum, mainly to Pakistan and India. At Modern Steel's plant, shredded scrap initially sourced from Russia will be preheated to around 400 degrees Centigrade and then melted in an Electric Arc Furnace to produce high grade mild steel (MS). A casting unit will thereafter churn out billets to the customer's specifications.<br>
                    <br>
                    Sreedharan says the billets, which are refined to BS-4449 Grade-460 standards, can be used to make a variety of steel productsincluding rolling products such as MS rods, channels, sections and flats for the construction industry, or seamless pipes for the lucrative hydrocarbons sector. Forging units can also convert the billets into high-value flanges for pipelines, or produce a variety of machinery or automobile parts.<br>
                    <br>
                    Company officials say they expect the plant's entire annual production of 76,000 metric tonnes of billeted steel to be consumed locally. Potential candidates are Sohar-based Sharq Sohar Steel Rolling Mill, which has an installed capacity of 180,000 metric tonnes, and Hadid Majan LLC, a new rolling mill currently under construction at Rusayl. Total demand for billeted steel in the UAE alone is estimated at 1.15 million metric tonnes per annum, 80% of which is imported.<br>
                    <br>
                    Depending upon market demand, Modern Steel can also switch to the manufacture of carbon and alloy steel for the forging industry. Stainless steel production is also an option, says Sreedharan. Plant construction and commissioning are being undertaken by Larsen &amp; Toubro (Oman), a subsidiary of the Indian engineering giant L&amp;T, which has built a number of giant steel plants across India. The plant is due to commence production in December 2000.</font></p>
