                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">In the Spotlight</font><p><font face="Verdana" size="1"><b>Interview with OIB's acting CEO Nani B Javeri.<br>
                    Oman International Bank (OIB) has made some bold decisions lately, not least the termination of its contract with chief executive Anthony Wright. OER caught up with the new man at the bank's helm, Nani B Javeri&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    </font><font face="Verdana" size="2"><img border="0" src="../../images/2001_september_october/spotlight.jpg" align="left" hspace="5" vspace="5">Like most banks in the Sultanate, Oman International Bank has found the last 12 months hard going. It reported a 9.4 per cent drop in net profit last year, and a 36.8 per cent decline in the first half of the current year. In July, OIB terminated the contract of its chief executive, Anthony Wright, and appointed general manager Nani B Javeri as acting CEO. In an exclusive interview with
                    OER, Mr Javeri gives a frank assessment of the challenges facing Oman's banking sector and speaks of OIB's strategy to weather the storm.&nbsp;<br>
          <br>
          <b>OER:</b> Could you comment on the departure of Tony Wright?<br>
          <b>Mr Javeri:</b> I have nothing more to add to the matter of the departure of Mr Wright.<br>
          <br>
          <b>OER:</b> All banks in Oman have seen their profits fall over the last 12 months. What is OIB doing to minimise the fall and return to profit growth?<br>
          <b>Mr Javeri:</b> Our strategy is to focus on quality and not quantity, in both assets and liabilities. We believe that growth of assets has attendant problems, such as deterioration in asset quality and the requirement of capital, and that return on assets, return on capital, cost/income ratio etc are better benchmarks of performance than asset growth. Rather than looking to increase profitability through higher net interest income, we are looking to generate more fees from new and expanded services in the areas of treasury, investment banking, private banking, credit cards etc. We are also focusing on the reduction of transaction costs through higher levels of automation and migrating customers to electronic banking channels, thereby increasing our share of automated transactions of 70 per cent.<br>
          <br>
          <b>OER: </b> What are the main challenges that lie ahead for OIB and Oman's banking industry?<br>
          <b>Mr Javeri:</b> The main challenges facing OIB are: booking quality assets, making recoveries from classified accounts, improving cost-income ratios through higher productivity, greater automation, and centralisation of processing, enhancing a 'sales' culture by removing processing functions from branches and allowing the branch staff to spend more time on customer service and sales, especially cross-selling of the full range of our services, and finally, providing a consistently high level of customer service quality.<br>
          <br>
          <b>OER: </b> Have we seen the worst of the 'bad loans headache', or has it only just begun?<br>
          <b>Mr Javeri: </b> Given the slowdown of the economy over the last two years and the impact of the fall in the
                    MSM, it is to be expected that asset quality would suffer. However, one has to bear in mind that this period was preceded by a rapid growth in assets, for the entire banking system, during the boom period of the mid 90s. This is a normal phenomenon that occurs the world over. Companies which do not have the inherent strengths to ride through dull patches in the economy are inevitably affected, and this invariably impacts on the banks. We believe that the cycle will work its way through in a reasonably short time.<br>
          <br>
          <b>OER:</b> Why has IAS 39 been such an issue for the banking industry in Oman?<br>
          <b>Mr Javeri:</b> IAS 39 is not a major issue for banks in Oman which are already governed by prudent provisioning norms laid down by the Central Bank of Oman. As for
                    OIB, we have consistently adopted the policy of providing as required by the
                    CBO, or by our external auditors, whichever is higher. Hence IAS 39 has little impact on our bank. However, some other banks who have concentrated on lending against security alone, rather than the borrower's inherent financial strength, may be affected by the IAS 39 provisioning requirements.<br>
          <br>
          <b>OER:</b> Are there any policy issues that could to be addressed to help the banking sector?&nbsp;<br>
          <b>Mr Javeri: </b> The policies of the CBO are fundamentally sound and designed to maintain the financial strength of the banking system. However, there are a few areas where we would like to see some amendments in the policies, such as:&nbsp;<br>
          - The cap on personal lending, and the ceiling of 13 per cent on the interest rate on such lending, has affected the profitability of banks. While we are in complete agreement with the CBO's view that reckless consumer lending should be curbed, we believe that such curbs should be by way of limits on exposures to an individual, by limiting loans to a reasonable multiple of salary, rather than an overall cap on the loan portfolio. The present system prevents younger people, who are starting off in life, from getting adequate financial assistance because the bank has reached its overall lending limit.<br>
          - The CBO's norms regarding mismatches in the maturity patterns of assets and liabilities should be based on behavioural norms as well and not only on contractual terms as the former provides a more realistic picture.<br>
          - Housing loans should be excluded from the overall consumer lending portfolio because building a house is a developmental expenditure rather than pure consumption expenditure. Construction of houses also boosts the economy as the bulk of the materials used are made in Oman.<br>
          <br>
          <b>OER: </b> In your opinion, are there too many banks in Oman, and do you think we will see more bank mergers?&nbsp;<br>
          <b>Mr Javeri: </b> It is a worldwide trend for banks to merge and Oman cannot be an exception in the long run. Mergers also provide economies of scale by spreading expenses on essentials, such as computer systems, over a larger business base. We therefore expect to see more mergers in the future. However, we are not aware of any merger at the moment.<br>
          <br>
          <b>OER:</b> What is your reading of the state of the economy? Do you see the domestic economy starting to pick up soon?&nbsp;<br>
          <b>Mr Javeri:</b> We believe the economy is picking up as the Government, having replenished the country's reserves, begins to spend money on projects and development. The downstream effect of the large projects on hand will begin to improve the economy, especially in key sectors such as construction. The impact will be better felt in the first half of 2002.&nbsp;<br>
          <br>
          <b>OER:</b> To what extent do you think Oman's economy will be affected by the global economic slowdown?<br>
          <b>Mr Javeri:</b> We see no strong reason for Oman to be affected by the global economic slowdown. As long as oil prices remain firm, the prospects of which seem to be good, Oman's financial position will remain good.  As non-oil exports are still relatively small, the global economic slowdown will not create a major impact on Oman. Indeed, Oman may benefit if world prices remain low as the cost of imports into Oman will remain low, too.<br>
          <br>
          <b>
          OER: </b> Thank you.</font></p>

