                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Coming home to
                    Roost&nbsp;</font><p><font face="Verdana" size="1"><b>Omanis are being asked to buy shares in the Sultanate's biggest and first fully-integrated poultry project. OER takes a look at why the promoters of A'saffa Poultry Farms SAOG are counting their chickens</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    <img border="0" src="../../images/2001_september_october/roost1.jpg" align="left" hspace="5" vspace="5" width="199" height="376"></font><font face="Verdana" size="2">Each year, the average Omani eats 25 chickens. Collectively, the Sultanate consumes more than 50 million chickens annually. As its population grows and its eating habits change, that figure is projected to rise.<br>
                    But of the 60,000 tonnes of chicken meat devoured each year, 70 per cent is imported.<br>
                    And that's why the promoters of a massive chicken farm in the Dhofar desert reckon they're onto a winner.<br>
                    A'saffa Poultry Farms, to be built at Thumrait in the southern Dhofar region, is one of the biggest agro-related investments in the Sultanate. The RO11.5 million project, which is spread over 50 square kilometers, is expected to be operational by early 2003.<br>
                    There will be nine farms, each with six broiler houses capable of producing nine million chickens, or 11,000 tonnes of meat, each year. Some 90,000 parent birds will lay up to 12 million eggs a year.<br>
                    The fully-integrated project has its own veterinary department, slaughter houses, feed mill, freezing facilities, power station, laboratory, cold storage and effluent treatment plant.&nbsp;<br>
                    Project promoters Al Baraka Economic Consultancy say the chickens, which will take about six weeks to mature, will not be fed hormones and will be housed in environment-controlled units.&nbsp;<br>
                    "Processing will be as per Islamic traditions," said Al Baraka general manager Nelson Mathew. He said the chickens would be priced competitively, and had the added attraction of being fresher than their imported rivals.&nbsp;<br>
                    The company expects to achieve turnover of RO3.4 million in its first year of operation, rising to RO5.6 million in the second year.&nbsp;<br>
                    According to a feasibility study conducted by Al Baraka, the poultry meat market in the Sultanate depends largely on imports. Despite efforts to develop chicken meat production by encouraging medium and small farms, the country imports 70 to 75 per cent of its requirement.&nbsp;<br>
                    The import of poultry meat was hovering around 43,000 tonnes (valued at RO19.7 million) in 1999, which was mainly from Europe and Saudi Arabia, while the UAE is a major re-exporter of poultry meat to Oman.<br>
                    With only four medium-scale farms in Oman at present, local production is insignificant when compared to the size of the import. There are 340 small-scale farms but their output is negligible.<br>
                    The feasibility study found that domestic demand for chicken meat, which was 58,000 tonnes in 1999, is projected to grow at four per cent per annum.&nbsp;<br>
                    This reflects annual population growth of 3.5 per cent and an incremental annual increase in consumption of 0.5 per cent - driven to some extent by the recent scares over red meat.<br>
                    Although exports to other GCC countries are possible, A'saffa Poultry will mainly focus on the domestic market.&nbsp;<br>
                    The company is aiming for an eight per cent share of the domestic market in the first year of operation, rising to 13.6 per cent by 2007, when total annual demand will reach 79,500 tonnes.&nbsp;<br>
                    The RO11.5 million required to finance the project will come by way of RO5 million equity and a RO4.4 million soft loan from the Ministry of Agriculture and Fisheries (MoA), with the balance in deferred payment and commercial loan from financial institutions.<br>
                    Of the RO5 million equity, the promoters will contribute RO2.5 million and RO2.5 million will be raised by way of an initial public offering (IPO). The IPO of 2.5 million shares - priced at RO1 each plus expenses of 20 baisas - opened on August 15 and closed on September 15.<br>
                    Brokers said institutions were likely to be the main subscribers, with individual investors still wary of the depressed Muscat Securities Market. The promoters are underwriting the issue.&nbsp;<br>
                    Two major pan-Arab financial institutions - Kuwait-based Gulf Investment Corporation (GIC) and the Sudan-based Arab Authority for Agricultural Investment and Development (AAAID) - will each take a 20 per cent equity stake.&nbsp;<br>
                    Local promoters (who will have a combined 10 per cent stake) are the Royal Office Pension Fund, Oman Gulf Company, Al Baraka Investment Co and the Global Omani Investment Holding Co.&nbsp;<br>
                    Initially, the project will give direct employment to 334 people, of which 120 will be Omani nationals.</font></p>

