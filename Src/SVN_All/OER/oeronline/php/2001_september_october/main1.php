                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Football Business</font>

                    <p><font face="Verdana" size="1"><b>The Sultanate's historic advance into the second World Cup qualifying round could help transform domestic football into a profit-making business. But is the private sector ready to play? Nayl D'Souza reports</b></font><font face="Verdana" size="2"><br>

                    <br>

                    <img border="0" src="../../images/2001_september_october/football1.jpg" align="left" hspace="5" vspace="5" width="225" height="308">When striker Majdi Shaaban headed in Oman's second goal to clinch victory against Syria in a decisive Asia Zone Group One qualifier on May 25, the Sultanate's dream of making it to the World Cup took a giant leap towards reality. The win saw Oman advance into the second qualifying round for the World Cup finals being held in Japan and South Korea next year - its best chance ever of booking a berth at the greatest sports show on earth.<br>
                    With public support for the senior team reaching new heights, the time is ripe for business to invest in Omani football - not only for the marketing power it can offer, but also to play a role in the development of the national sport.<br>
                    Investment, however, is proving hard to come by.<br>
                    As the Sultanate forges ahead with its economic diversification and accelerates its privatisation programme in the fields of power generation, telecommunications, aviation, education and health, sport is one sector of the economy relatively void of private enterprise.<br>
                    Oman's national teams at all levels have yet to find a long-term sponsor for next season. Of the 52 clubs in the national league - a league that is not sponsored and lacks advertising hoardings at most grounds - only a handful of teams have secured a commercial partner and the sums of money involved are relatively small.&nbsp;<br>
                    <img border="0" src="../../images/2001_september_october/football2.jpg" align="right" hspace="5" vspace="5" width="200" height="268">The low level of private sector support has meant clubs have remained impoverished and reliant on private individuals to come to the rescue with donations.&nbsp;<br>
                    "Until now the private sector has not reacted positively to the sport because perhaps it is not aware of how important the game is," says Sheikh Saif Hashil Al Maskery, the President of the Oman Football Association (OFA).&nbsp;<br>
                    "But football is the ultimate medium to generate exposure on a local, regional and international level. I feel that as the sport becomes more professional in nature, the private sector will definitely find football a more viable and attractive option to invest in."&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><b>Searching for local heroes</b><br>
                    <br>
                    There is no doubt that Oman is a football-crazy nation. Almost anywhere you go in the Sultanate you will find boys playing soccer, usually in bare feet on makeshift pitches in the sand. But translating raw enthusiasm for the sport into a structured, profit-making enterprise is another matter.&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_september_october/football4.jpg" align="left" hspace="5" vspace="5" width="207" height="300">Despite free admission to football matches in Oman, the OFA still struggles to fill its stadiums. Yousuf Obeid Khatib, Oman's under-17 goalkeeping coach, sums up the situation: "There are no big crowds watching the games or paying gate money. In turn this does not attract sponsors with money and it will be very difficult for Oman's clubs to run as profitable businesses."<br>
                    John Adshead, coach of the Sultanate's under-17 team, says finding a local football hero would go a long way to changing all that.&nbsp;<br>
                    "All we need now for the thousands of Omani kids playing football is a local hero to worship," he says. "If we can establish one player overseas then I'm sure the boom would start."<br>
                    If the Sultanate reaches the World Cup finals, the exposure given to individual players would increase, and so would their chances of being scouted by international clubs. The hope of industry officials, coaching staff and the majority of fans is that some will play abroad, sooner rather than later.<br>
                    Oman's star striker and leading goal scorer, Hani Al Dhabit, is probably the best-known role model to inspire young wannabes at the moment. He was voted Asian Player of the Month in May by the Asian Football Confederation.&nbsp;<br>
                    There are two things on his wish list: "I want the national team to get through to the Cup finals and I want to be a professional footballer," he says.&nbsp;<br>
                    Within two years he expects to play abroad and earn a modest income, initially in the United Arab Emirates or Saudi Arabia. "Basically, I want to play in any country with a competitive league."&nbsp;<br>
                    Playing in the GCC for a premier league club, Hani can expect to net about RO1,500 per month excluding housing and transport allowance. This is higher than the average salary of around RO550 per month that foreign players from elsewhere in the Gulf and Africa are being paid to play football in the premier clubs of Oman.&nbsp;<br>
                    But it is a far cry from the salaries of the top players in England's Premier League. They take home around £35,000 per week (RO78,000 a month), while Manchester United stars David Beckham and Roy Keane are rumoured to have signed new contracts with the world's richest football club topping £100,000 per week (RO224,000 a month), excluding player bonuses, sponsorships and endorsements.</font></p>

                    <p align="center"><font face="Verdana" size="2"><img border="0" src="../../images/2001_september_october/football3.jpg" width="400" height="276"></font></p>

                    <p><font face="Verdana" size="2"><b>Unlocking Oman's football wealth&nbsp;</b><br>
                    <br>
                    The Government gives the OFA RO700,000 (US$1.82 million) a year in funding. In comparison, the level of recorded investment that Spanish club Barcelona spent on developing homegrown soccer talent was over US$5 million in 1999. Yet the relatively modest amount of money invested in grassroots soccer in recent years is already yielding dividends.&nbsp;<br>
                    Haroon Amur Al-Bartamani, goalkeeping coach for Oman's under-19 national team, is adamant that the Sultanate is developing better players than it used to.&nbsp;<br>
                    "There are many pluses in how the local game is developing," he says.&nbsp;<br>
                    "We have many more internationally trained coaches than before, we have set up youth academies around the country at different clubs, and teams now play more competitive games in a season. There's also a greater focus on improving the technical ability of players in different age groups, especially school kids."&nbsp;<br>
                    This month, Oman's under-17 team travels to the junior World Cup in Trinidad and Tobago having qualified for its third appearance.<br>
                    Adshead says Oman has a wealth of football talent just waiting to be unlocked.<br>
                    "As a football development country, we have a rough-cut diamond," he says. "If we can get a hold of it properly, shape it and polish it, then the country would be extremely wealthy in football terms.<br>
                    "The youth development programme means that Oman has already identified players who could be representing Oman at senior level in 2010. So my job is to set up a production line that keeps on popping out quality performers."<br>
                    But Adshead, who previously steered New Zealand's senior team to the World Cup finals in 1994, says success will also depend on Oman's ability to secure a financial future for the sport. "There is no point training up 10-year olds when later their development programme is kicked from underneath them. We've got to generate revenue at home to run our academies. We've got to become self-sufficient. We've got to pump our own oil."&nbsp;<br>
                    He adds: "What we can offer companies is more than their name and products up in lights. Afterall, there is no greater trade delegation in the world than a football team."<br>
                    <br>
                    <b>Five-year plan</b><br>
                    <br>
                    Fahad Abdullah Al-Raisi, OFA's general secretary, says the OFA is implementing a five-year plan to develop football in Oman and turn it into a profitable business.&nbsp;<br>
                    "Even though we are in a better position than many other sports in Oman, we have to increase our efforts to build better awareness and co-operation from the private sector," he says. "We have already begun to implement a five-year plan to develop the sport and we see the level of interest picking up as companies begin to appreciate the value there is in the game."<br>
                    The development plan has kicked off with the establishment of 24 youth academies across the country. Each academy encourages local youth participation in football and has more than 30 players and paid staff consisting of coaches, dieticians and medics to assist the youngsters to get to the top of the sport. The OFA will receive US$1 million funding between 1999 and 2002 from Fifa's 'Goal' programme to run these academies.&nbsp;<br>
                    Last season the OFA scrapped regulations governing transfers that prevented nationals from leaving the country to play football abroad. It has also introduced measures to promote mergers between clubs and has plans to increase the number of teams in the premier division from 10 to 12.<br>
                    Aside from youth training, the early focus of the OFA's five-year plan has been to develop an efficient administrative base for the sport to rely upon. However, it is now keen to develop the marketing side of the sport.<br>
                    <br>
                    <b>Marketing football&nbsp;</b><br>
                    <br>
                    The OFA has employed a sports marketing company to help it secure sponsorship. Bahrain-based Middle East Events Marketing (MEEM) has Gulf-wide experience of promoting football, having successfully secured big sponsors for the Qatari and Bahraini national teams. It is now developing Oman's domestic league into a promotional vehicle for private companies.&nbsp;<br>
                    According to MEEM general manager Nasser Al Hashmi, one major obstacle that could stifle the development of football in Oman is the lack of media coverage given to the sport.&nbsp;<br>
                    "The media are the lifeblood of any sport," he says. "If my club doesn't receive media coverage, how can I market my players and what benefits will the companies who sponsor my team receive?" But Al Hashmi sees a bright future for soccer in Oman.&nbsp;<br>
                    "In our short time as promoters of Oman football, we have built up the level of media coverage and increased the number of spectators at matches," he says. Last season there were 17 matches live on Oman television. This coming season, the number is pegged to rise to 28.&nbsp;<br>
                    At present, admission to football matches is free in Oman. But MEEM has introduced RO1 soccer scratch cards offering prizes, which it hopes will introduce fans to the concept of paying to see their favourite sport. Regardless of sponsorship, supporters will have to start paying at the turnstiles at some stage if football is to become a viable business. OFA officials say they are tentatively planning to introduce charges at internationals at some point in the future.<br>
                    Over the next five seasons, MEEM is contracted to give the OFA US$8 million in promotional income. Meanwhile, the OFA will earn $100,000 for the television rights and use of advertisement hoardings for each of the four home fixtures in the second round World Cup qualifiers.&nbsp;<br>
                    <br>
                    <b>Desperately seeking sponsors</b><br>
                    <br>
                    Most companies OER contacted viewed football with enthusiasm. But despite the tremendous opportunities available in football, no single company has claimed control of the medium and most seem happy to delay making any tie-ups.<br>
                    Sanjay Kumar, retail brand communications manager for Shell Oman Marketing Co., says it is only a matter of time before corporates are vying for ownership of the sport in Oman.&nbsp;<br>
                    "Its value lies in the high participation of a key target group - young people that make up some 62 per cent of the population," he says. "Football's role in future promotion is a very important marketing proposition."<br>
                    Shell's global sports sponsorship revolves mainly around Formula One's Ferrari team, but locally it co-sponsored His Majesty's Cup football tournament last season.&nbsp;<br>
                    Khalfan Rashid Al Tal’ey, Oman International Bank's (OIB) deputy general manager, says: "The success and achievement of the clubs and the national team will motivate all corporates and not only OIB to get involved more and support the development of football in Oman through sponsorships." He says OIB has supported many different clubs on many occasions in the past.<br>
                    Ali Al Hashar, Oman LNG's public affairs officer, says that last season OLNG gave RO5,000 to Al Aruba to put the company's logo on its players' t-shirts. He says that there is currently no intention for the company to sponsor the Sur-based club this coming season, although "discussions with local clubs for sponsorship are on-going".&nbsp;<br>
                    Meanwhile, the National Bank of Oman (NBO) has launched a World Cup MasterCard to promote its brand alongside Oman's national team without officially informing the OFA or making any substantial financial commitment to the senior team.&nbsp;<br>
                    "They (NBO) are using the team to promote their brand and at least they should have sat down with us first to discuss this beforehand," says OFA general-secretary Al-Raisi. "They're using the victories of the team for promotion and this is not nice, if not immoral."&nbsp;<br>
                    NBO deputy general manager Abduaziz M Al Balushi says the MasterCard promotion targets the fans of the national soccer team.<br>
                    "Had it involved the team or the Oman Football Association (OFA) directly, we would definitely have approached the OFA," he says, adding that the campaign "helped raise the profile of our national football team and its sterling performance in the World Cup".<br>
                    Al Balushi says a substantial portion of NBO's promotional budget is channelled into sports sponsorship. "We have given financial support to almost all the football clubs in Oman at one time or another."&nbsp;<br>
                    <br>
                    <b>Reality bites</b><br>
                    <br>
                    In the coming months, the hopes of the nation will either be dashed or its dreams fulfilled. If Oman's senior football team progresses to next year's World Cup finals, it will bring acclaim to the Sultanate. Aside from the kudos, it could also bring huge financial rewards to the sport and the local economy.<br>
                    Some people will give the rookie team little chance of success, and certainly the odds are stacked against them. But even if the team doesn't make it, it has still progressed further than ever before. And let's not forget the junior team's success.&nbsp;<br>
                    Soccer in Oman is on the up. The players and coaches are doing their part - now it's time for business to enter the fray.&nbsp;</font></p>

