                          UAE, Yemen and/or Qatar. Other commentators feel vast improvements to current facilities and stadiums are required before Oman could host lesser tournaments such as the Asia Cup.&nbsp;<br>
                          <b>Attendance:</b> Domestic and international soccer matches are dogged by poor attendance figures. The average gate at a local game is below 2,000 and first division matches have been known to pull crowds of less than 100.&nbsp;<br>
                          The national team had an average spectatorship of 5,000 for its first round World Cup qualifiers. However, as Oman's chances of reaching the finals improved, so has its support at matches. The crunch game against Syria in May produced a record attendance of around 30,000. Moreover, an estimated 14,000 fans witnessed the friendly tie against Iraq at the end of July - a record for a friendly regional fixture.<br>
