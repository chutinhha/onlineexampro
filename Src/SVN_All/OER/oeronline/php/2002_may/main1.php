                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Making sense of
                    a senseless market</font>
                    <p><font face="Verdana" size="1"><b>With a host of political uncertainties hovering around the Middle East and the world in general, oil prices seem to be kept under constant pressure, waiting for a change, writes Patrick Dane</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_may/oil.jpg" align="left" hspace="5" vspace="5" width="225" height="169"></font><font face="Verdana" size="2">Who’d be an oil trader? In recent weeks they’ve had to grapple with war in the West Bank, a planned US invasion of Iraq and the fall and rise of Venezuelan president Hugo Chavez. And that’s just politics. Couple all that with one of the most unpredictable global economic outlooks for decades, and it’s clear that these are tough times for the army of traders and analysts who buy, sell, swap and bet on oil and its myriad financial and physical derivatives.<br>
                    “These are crazy times,” says one analyst on a major London-based energy team. “We’ve raised our oil price forecasts by more than 20 per cent already this year, and they’ll probably change again. But it’s not easy trying to forecast in which direction the markets will move, let alone how far. You can you have all the clever econometric models you like, but they can’t tell you when George Bush will roll into Baghdad, or who’s going to be in charge in Caracas [the capital of Venezuela] when you wake up in the morning.”<br>
                    Politics and oil markets are inextricably linked. It’s impossible to be precise, but most insiders believe that up to US$7 of the US$27 a barrel price of Dated Brent crude in mid-April was down to political risk premiums. Because political events change overnight, prices on international markets have been gaining and losing up to 10 per cent of their value in one day throughout much of 2002 - historically, that is extreme volatility.&nbsp;<br>
                    All this has been compounded by unprecedented economic uncertainty. “The big unknown in the oil market at this time is the timing and strength of the American economic recovery,” says Dr Randa Azar Khouri, chief economist of National Bank of Kuwait (NBK). Oil demand growth is driven by economic growth, and the US accounts for some 25 per cent of global daily oil demand (currently around 74 million barrels per day). As such, US growth is a key factor in the oil price equation. But forecasting United States’ economic performance is mystifying economists. As recently as January, most were predicting a full-blown recession. Now, many argue that America is booming.<br>
                    “The recovery is projected to be led by the United States, where GDP growth is expected to be 2.3 percent in 2002,” said Kenneth Rogoff, Economic Counsellor and Director of Research at International Monetary Fund (IMF), in late April. But he admitted that the Fund’s forecast is shrouded in uncertainty. “It could even turn out higher,” said Mr Rogoff. Others worry that it may be lower. The US has shown impressive signs of recovery in recent months. But some economists fear the country may experience a “double dip” recession, and that the worst is yet to come.&nbsp;<br>
                    Certainly, unusually complex factors are at work - a fact acknowledged by the Paris-based International Energy Agency (IEA) in its April Monthly Oil Market Report. “Driven by oil market fundamentals and a geopolitical risk premium, crude oil prices surged by US$7 to US$8 per barrel from the last week in February to the first week in April. The fundamentals included the US economic recovery, falling US product stock, strong US gasoline demand and reduced OPEC supply. The risk premium was centred mainly on Iraq and the Israeli-Palestinian conflict.”<br>
                    Given all this uncertainty, what is it the best guess of analysts on the likely direction of oil markets for the rest of 2002? Opinions differ, but the consensus seems to be that prices will hold on to the most, if not all, of their recent gains.<br>
                    “A stronger sentiment on the side of OPEC production discipline, a growing sense by the market that economic growth may accelerate more rapidly than previously thought, and a continued uncertainty surrounding tensions in the Middle East have elevated near-term prices above previous expectations,” says the US Government’s Energy Information Administration (EIA) in its April Short Term Energy Outlook.&nbsp;<br>
                    “This has caused us to raise expected average West Texas Intermediate prices for 2002 by about US$2 per barrel from last month’s projected US$22.80,” adds the EIA. “Continued strong compliance by OPEC producers to meet current quotas through the second quarter of this year, and continued momentum toward economic recovery in 2002, would be important factors in maintaining and building strength in prices through 2002.”&nbsp;<br>
                    Certainly, traders seem to think that markets will be tight throughout the rest of the year. Forward prices are mainly above spot prices - a clear indication that the market thinks the recent rally is sustainable. “A scenario with WTI prices persistently below the US$20 per barrel level is considered to be very unlikely in the short-to-medium term,” says the EIA. (They dipped to around US$17 a barrel in November, when the post-September 11 economic gloom reached a crescendo, having reached a 12-month high of US$31 a barrel on the day of the attacks as panic swept through markets).<br>
                    But all current forecasts are written in sand, not stone. “Uncertainty about the extent of a non-OPEC production response, worldwide economic strength and other factors, however, result in a broad range of uncertainty concerning potential price outcomes over the next seven quarters,” says the EIA. Oil traders are known to make some of the biggest salaries in the industry. This year, they will have to earn them.&nbsp;</font></p>
