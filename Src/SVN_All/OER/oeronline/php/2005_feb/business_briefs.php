                    <p align="justify"><font face="Verdana" size="1"><b>AES 
                    Barka IPO receives overwhelming response</b><br>
                    AES Barka SAOG—the largest independent power and water 
                    generation company in the Sultanate of Oman on a BOO (Build 
                    Own Operate) basis—has received an overwhelming response 
                    from both individual and institutional investors for its 
                    initial public offering (IPO), which closed on December 20, 
                    2004. <br>
                    <br>
                    The RO11 million IPO, for which BankMuscat was the financial 
                    advisor and lead manager, was oversubscribed over 17 times 
                    with gross collections in excess of RO189 million from more 
                    than 13,000 applicants, making it one of the most successful 
                    IPOs in the Sultanate to date. Sharing his views on the 
                    successful closure of the issue, Larry Cantrell, CEO of AES 
                    Barka said: “We are committed to living up to our promise of 
                    creating a successful project, that will provide valuable 
                    energy resources to the Omani economy.”<br>
                    <br>
                    The issue, which was in keeping with a contractual 
                    commitment made by the original promoters of the project to 
                    the government of Oman had offered 35 per cent of the share 
                    capital, the equivalent of around 11.2 million shares of 
                    AESB held by AES Corporation, the US (through its investment 
                    companies) and Multitech LLC, a Suhail Bahwan Group Company, 
                    to the investing public. Priced at RO0.970 per share (plus 
                    20 baizas towards issue expenses), the issue was open to 
                    subscription from Omani nationals and Omani incorporated 
                    entities.<br>
                    <br>
                    Commenting on the success of the issue, Abdul Razzak Ali 
                    Issa, CEO, BankMuscat said: “We are glad that the investing 
                    public came back in such strong numbers to support the AES 
                    Barka IPO. Such investor sentiments can only augur well for 
                    organisations with strong fundamentals and carefully thought 
                    out business models that are planning to go public. With a 
                    number of IPOs in the offing including the much-awaited 
                    Omantel issue, Omani investors have much to look forward to 
                    in the New Year.”</font></p>
