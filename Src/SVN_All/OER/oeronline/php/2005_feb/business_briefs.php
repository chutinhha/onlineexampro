                    Oman to get its first iron and steel plant</b><br>
                    <img border="0" src="im43.jpg" align="left" hspace="5" vspace="2" width="200" height="99">Last 
                    month, Oman marked the beginning of a new era to augment 
                    industrial growth by signing three long-term agreements with 
                    a leading business group in the UAE to set up the nation’s 
                    first iron and steel plant. The agreements will pave the way 
                    for the UAE business group to set up one of the most modern 
                    iron and steel plants in the world, in Sohar. In three 
                    phases, the project will entail a total cost of $750 million 
                    (RO288.50 million). <br>
                    <br>
                    The agreements included an agreement on seawater intake to 
                    cool the project with the ministry of national economy; an 
                    MoU on gas supply with the ministry of oil and gas; and a 
                    land utilisation agreement with Sohar Industrial Port 
                    Company (SIPC). Al Ghaith Holding, one of the leading 
                    business groups in the UAE, will construct and manage the 
                    project. The plant, which is coming up in the Sohar 
                    Industrial Port, in technical collaboration with Midrex 
                    Technologies of the US and VAI Fuchs of Germany, will be the 
                    third iron and steel plant in the Gulf. <br>
                    <br>
                    For Oman, the agreements were signed by HE Ahmed bin 
                    Abdulnabi Macki, the Minister of National Economy and Deputy 
                    Chairman of the Financial Affairs &amp; Energy Resources 
                    Council; HE Shaikh Mohammed bin Abdullah al-Harthy, the 
                    Minister of Transportation &amp; Communications and chairman of 
                    SIPC board; and Nassir bin Khamis al-Ghashmi, the 
                    undersecretary of Oil &amp; Gas. They were signed from the 
                    Shadeed Iron and Steel LLC (a company of al-Ghaith Group of 
                    Abu-Dhabi) which will implement the project, by Ali al-Ghaith, 
                    company board chairman and Ghaith al-Ghaith, company board 
                    deputy chairman. <br>
                    HE Macki said the project is one of the important projects 
                    to be implemented at Sohar Industrial Port. He noted that 
                    the project would actively contribute to the country’s GDP. 
                    He also said that the steel project with its three phases 
                    would create 585 job opportunities for Omanis. He said the 
                    volume of investments at Sohar Industrial Port would come up 
                    to $9.5 billion (RO3.65 billion) at the end of 2008. <br>
                    <br>
                    HE Shaikh al-Harthy said he was delighted that a major GCC 
                    investor has chosen to invest in Sohar. This investment and 
                    its related cargo flows will further strengthen the position 
                    of Sohar as an Industrial Port and through the economics of 
                    scale will make use of the port more economically for all 
                    present and future users and investors. <br>
                    <br>
                    It took more than seven months for the government, SIPC and 
                    the UAE firm to clinch these prestigious pacts. The first 
                    phase of the project will produce 720,000 metric tonnes of 
                    hot bricketted iron and steel billets per year. In the 
                    second phase, the capacity will touch one million metric 
                    tonnes per year. The total cost of two phases (2007-09) is 
                    estimated at RO350 million. <br>
                    <br>
                    Shadeed Iron and Steel will expand its production capacity 
                    to four million metric tonnes per year at the end of the 
                    third phase (2009-10). The project will occupy a total area 
                    of 116 hectares in three phases. Currently, Oman’s import 
                    bill constitutes significant steel imports. <br>
                    <br>
                    Shadeed steel project will make Oman a leading producer of 
                    iron and steel in the region. Advance Projects Development, 
                    the industrial arm of Al Ghaith Holding, will be responsible 
                    for managing the project and implementing local engineering 
                    and supply. The plant is expected to export iron and steel 
                    products to neighbouring markets like the UAE, India, China, 
                    etc. </font></p>

