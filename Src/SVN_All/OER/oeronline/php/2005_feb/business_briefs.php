                    <p align="justify"><font face="Verdana" size="1"><b>Mutual 
                    funds allowed to invest in real estate </b><br>
                    The Capital Market Authority (CMA) has approved regulations 
                    allowing mutual funds to invest in the country’s real estate 
                    market provided they seek prior permission from its board. 
                    This is part of new regulations that were approved by the 
                    board of the regulatory authority to encourage investors to 
                    park their funds in mutual fund schemes, CMA vice-president 
                    HE Abdullah Salim al Salmy said.<br>
                    <br>
                    So far, mutual fund schemes have been investing their corpus 
                    fund in either equity instruments or bonds listed on the 
                    Muscat Securities Market (MSM). In an apparent move to 
                    encourage investment in mutual fund schemes, he said that 
                    the government has given income tax exemption to mutual 
                    funds through a recent Royal Decree.<br>
                    <br>
                    HE Al Salmy said that the new measures, which are circulated 
                    among investment companies, are aimed at regulating mutual 
                    funds in such a way that they could enhance the confidence 
                    of investors. ‘‘The funds of investors in mutual fund 
                    schemes are looked after in a proper manner. These measures 
                    are aimed at making the regulations easy for all parties 
                    concerned with mutual fund schemes – fund managers, 
                    custodians and investors,’’ he noted. <br>
                    <br>
                    Elaborating on the advantages of mutual fund investment, he 
                    said that it would enhance saving habit in the country, 
                    which is necessary for economic development. This, he said, 
                    would encourage those who have small savings potential to 
                    indirectly invest in the stock market, which in turn would 
                    check conspicuous consumption of people. Unlike investments 
                    in real estate, mutual fund schemes are easy to liquidate, 
                    he noted.</font></p>
