                    <p align="justify"><font face="Verdana" size="1"><b>Dhofar 
                    Power’s IPO this month</b><br>
                    Dhofar Power Company (DPC) is expected to offer 35 per cent 
                    of its stake to investing public this month. PSEG group, the 
                    US-based multinational, has an 81 per cent stake in the 
                    company while the remaining 19 per cent shares are held by 
                    local investors. Sources at BankMuscat, the issue managers, 
                    said that around 10 million shares constituting 35 per cent 
                    of the issued capital of DPC would be on offer through an 
                    IPO before end of February. The offer price is yet to be 
                    finalised. The pricing principle of all independent power 
                    companies is same. <br>
                    <br>
                    The issue is for fulfilling a contractual commitment made by 
                    the founders to the government at the inception of the 
                    project. DPC will be the fourth power firm and the third one 
                    working on a build, operate and own (BOO) basis to be listed 
                    with its peers - Al Kamil Power and AES Barka. There has 
                    been tremendous response to the IPOs that were floated 
                    recently by two other independent power projects. The recent 
                    issue of AES Barka was oversubscribed by 17 times. <br>
                    <br>
                    DPC, which has a 240-MW power plant in Salalah, will 
                    eventually take over the existing power facilities from the 
                    ministry. The Salalah project, which started commercial 
                    operation sometime back, was implemented as part of the 
                    overall privatisation of the power system in that region.
                    <br>
                    <br>
                    Unlike other privatisation projects undertaken to date, 
                    Salalah power project involves the whole power system, 
                    covering generation, transmission and distribution, and for 
                    the first time will involve a private company selling power 
                    to end-consumers.</font></p>
