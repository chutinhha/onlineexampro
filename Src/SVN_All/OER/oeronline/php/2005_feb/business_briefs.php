                    <p align="justify"><font face="Verdana" size="1"><b>CBO cuts 
                    interest rates on personal loans </b><br>
                    The Central Bank of Oman (CBO) has decided to reduce 
                    interest rate ceiling on consumer loans. The CBO had earlier 
                    decided to cut by one percentage-point the bank’s maximum 
                    charge on personal loans – from 10 to 9 per cent. HE Hamood 
                    bin Sangour Al Zadjali, the executive president of CBO, said 
                    the decision was made as a result of the decline in the 
                    value of the interest rate paid on deposits and the strong 
                    liquidity and profits in the banking sector. <br>
                    <br>
                    He said the reasons behind fixing a rate ceiling on personal 
                    loans were to prevent banks from charging higher rates and 
                    also to allow for competition without letting the ratio of 
                    personal loans exceed 42.5 per cent of the total banking 
                    credit to government and private sectors. <br>
                    <br>
                    HE Al Zadjali said it was the fourth time the CBO had 
                    reduced the interest rate ceiling on consumer loans. In 
                    early 2002, the central bank had cut the rate from 13 to 12 
                    per cent and increased the quantitative ceiling on personal 
                    loans from 35 to 40 per cent of the total bank credit. In 
                    April 2003, the CBO reduced further the rate ceiling by one 
                    percentage point to 11 per cent. <br>
                    <br>
                    The apex bank again cut the interest rate ceiling in January 
                    2004 from 11 to 10 per cent and raised the personal loan 
                    portfolio from 40 to 42.5 per cent of the total credit. HE 
                    Al Zadjali said the CBO revised annually interest rates in 
                    the local markets and elsewhere to decide whether the 
                    prevailing rate on personal loans was appropriate. <br>
                    <br>
                    He said the central bank would factor in operational costs, 
                    the level of risks and returns on shares when it decided on 
                    the loan interest rate ceiling. The executive president said 
                    the apex bank was aware of the negative effects of excessive 
                    consumer borrowing and urged licensed banks to change 
                    policies regarding maximum lending to individuals.</font></p>
