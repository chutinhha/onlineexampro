            <p align="justify"><font face="Verdana" size="2" color="#C0C0C0">::
                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">It’s 
            Playing it up!</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The Sultanate’s 
            historic advance into the finals of the GCC Cup has shown the way to 
            make sports a profit-making business. OER outlines ways and means 
            for different games to capture their fair share of the sports 
            market’s revenues and translate their popularity into cash</b></font></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im1.jpg" align="left" hspace="5" width="294" height="175">Members 
            of the national football team—although unlucky to have been edged 
            out 5-4 in the penalty shoot-out of the 17th Arabian Gulf Cup Soccer 
            Championship finals against Qatar in Doha—were accorded a hero’s 
            welcome on arrival to the capital. A great reception was held to 
            honour the players and the administrative crew at the Sultan Qaboos 
            Sports Complex in Bausher. The team was welcomed at the airport by 
            HE Shihab bin Tariq Al Said, the Advisor to His Majesty, HE Ali bin 
            Hamood Al Busaidi, the Minister of Diwan, HE Ali bin Masoud Al 
            Sunaidy the Minister of Sports Affairs, and the presidents of many 
            sport unions.<br>
            <br>
            Zubair Corporation presented 16 Skoda cars and RO1000 to all the 
            members of the Omani football team including the crew. Sheikh Suhail 
            Bahwan presented RO41,000. BankMuscat and Port Services Company gave 
            away RO32,000 apiece. WJ Towell awarded the entire team and their 
            crew with another RO16,000. Other prominent companies to honour the 
            national football team in cash and kind included Oman Mobile, Noor 
            Majan Watches, Shell, National Biscuit Industries, United Finance 
            Company, Gulf Engineering Consultancy, Raha Polyproducts, Unikai, 
            NBO, Gulf Air, Oman Cables and Oman National Transport Company.<br>
            <br>
            This kind of support and money pouring into the sport proves that 
            Oman is finally moving towards acknowledging sports as a business. 
            With public support for the senior team reaching new heights, the 
            time is ripe for business to invest heavily in Omani football – not 
            only for the marketing prowess it can offer, but also to play a role 
            in the development of the national sport.<br>
            <br>
            Sports in the region is currently enjoying a very high profile, as 
            governments actively push participation and many states bid to host 
            major sporting events to help them attract cash, rich tourists and 
            corporations to the region. Therefore, millions of dollars are 
            pouring into infrastructure projects and bids for sports events. 
            Oman recently announced the establishment of the Ministry of Sports 
            Affairs, with HE Eng. Ali Bin Masoud Al Sunaidy as the Minister. <br>
            <br>
            As the Sultanate forges ahead with its economic diversification and 
            accelerates its privatisation programme in the fields of power 
            generation, telecommunications, education and health, sport is 
            another sector of the economy where private enterprise is 
            increasingly playing an important role. <br>
            <br>
            The recently formed Ministry of Sports Affairs has earmarked a sum 
            of RO210,000 as budgetary allocation for Oman Athletics Association 
            (OAA) for the year 2005 and an additional sum of RO50,000 to finance 
            the Association’s external activities and to cut down the 
            Association’s deficit which amounted to RO53,000. OAA’s budget is 
            expected to reach RO285,000 after receiving an expected assistance 
            of RO25,000 from the private sector. As for the Oman Basketball 
            Association, the Ministry has made an allocation of RO125,000 in 
            addition to RO50,000 to cover external participations.<br>
            <br>
            Undoubtedly, sports today mean big business. In a world of cutthroat 
            business of sports, nerves of steel is what is required to compete. 
            Sport has become an international entertainment industry with global 
            revenues of more than $38 billion (RO14.62 billion) in television 
            rights and ticketing alone. <br>
            <br>
            The past 20 years have seen the emergence of sports as a major 
            service industry that shapes the investment attractiveness of cities 
            and regions and enhances their liveability. As it is so young and 
            dynamic, awareness of the potential of the industry within business 
            and government is still growing and industry analysis has yet to 
            develop fully.<br>
            <br>
            Global sports sponsorship is reckoned to have hit an all time high 
            of $28.32 billion (RO10.89 billion), up 8.1 per cent from $26.2 
            billion (RO10.07 billion) in 2003 and $24.4 billion (RO9.38 billion) 
            in 2002. Behind its appeal, sponsorship of sports is a 
            relationship-building tool that is concerned not just about brand 
            awareness but engages the audience both on a rational as well as an 
            emotional level. At its core is a commercial proposition that 
            provides a return on investment for both the brand owner and the 
            rights holder.<br>
            <br>
            <b>Games galore</b><br>
            Sportsmen aren’t the only contestants in the world of professional 
            sports: individual sports and, of course, sporting events, are 
            battling one another for a bigger slice of the increasingly 
            lucrative global sports industry. The worldwide economic downturn 
            triggered by 9/11 intensified the competition with other forms of 
            entertainment. Globally, traditionally popular sports like soccer 
            and American football enjoy an inherent advantage and are grabbing 
            the biggest slices of a competitive pie. <br>
            <br>
            These two sports combined rake in more than one-third of the 
            revenues in the global TV sports market (see The Winning Games). But 
            commercial success isn’t just about popularity. For a sport to 
            capture its fair share or more of this market’s revenues, it must 
            translate its popularity into cash. Some sports, regardless of size, 
            capitalise on their popularity more successfully than others do. <br>
            <br>
            While broadly popular sports such as soccer will fight to extend 
            their dominance, up-and-coming sports with niche appeal, from 
            professional cycling and shooting to ‘extreme’ sports, parachuting 
            and surfing, can try to improve their individual competitiveness by 
            studying what successful rivals have done.<br>
            <br>
            As the competition heats up, the owners and executives of teams, the 
            officials, the leagues—and, perhaps most important, the 
            international associations that could market sports on a local and 
            global scale—will have to go on increasing the attractiveness of 
            sports to consumers, broadcasters, and sponsors while also realising 
            their commercial value.<br>
            <br>
            <b>Resource roulette</b><br>
            The industry’s core sources of revenue can be divided into three 
            main categories: broadcast rights, sponsorships (including licensing 
            and merchandising), and ticketing and hospitality (such as 
            entertainment and catering in sports venues). Each category accounts 
            for roughly one-third of the industry’s revenues, and each has 
            enjoyed strong growth. According to a McKinsey study, total revenues 
            in the US from TV rights rose by 14 per cent a year from 1997 to 
            2001.<br>
            <br>
            In 2002, however, the economic downturn forced companies to cut back 
            their marketing budgets. The advertising revenues of the US networks 
            couldn’t match the amount they had paid to acquire broadcast rights 
            at the business cycle’s peak – so, they lost an estimated $4 billion 
            from sports programming that year. <br>
            <br>
            In addition, the rate of revenue growth from global corporate 
            sponsorships has fallen by 6 per cent annually since 1996. Thus, 
            sports increasingly compete among themselves for a tighter pool of 
            funds. To add to the industry’s woes, consumer interest seems to be 
            waning in the wake of other avenues of entertainment catching up. In 
            Europe, sports-TV viewership declined by 15 per cent between 1996 
            and 2001 as other forms of entertainment programming—most notably 
            reality TV—gained popularity. <br>
            <br>
            Sports have no choice but to respond: the Wimbledon tennis 
            tournament plans to put a roof over Centre Court to eliminate the 
            rain delays that wreak havoc with broadcast schedules, for example, 
            and the Australian Open has scheduled matches for prime time. 
            Licensees too are getting tougher, with many now demanding exclusive 
            deals.<br>
            <br>
            <b>Raising the bar</b><br>
            In such a rapidly evolving scenario, all sports organisations and 
            those associated with individual sports must continually raise the 
            commercial game plan of their respective sports. All sports, 
            regardless of whether they have a mass or niche appeal, must have a 
            stab at improving their competitiveness by carefully examining what 
            their successful counterparts have done and adapting their 
            strategies accordingly.<br>
            <br>
            Everyone concerned must try to make the sport more attractive to 
            consumers, advertisers, broadcasters, and sponsors. At the same 
            time, the challenge of translating a sport’s popularity into 
            revenues should also be dealt with directly. International sports 
            associations charged with boosting the commercial standing of their 
            sports have much to learn in this respect from powerful national 
            leagues such as the US-based National Basketball Association and the 
            National Football League.<br>
            <br>
            Decisions about which ones to employ, and in which order, must be 
            carefully tailored to the strengths and weaknesses of a sport and 
            applied with a long-term perspective that respects and maintains its 
            traditions.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Soccer scores</b><br>
            <img border="0" src="im2.jpg" align="right" hspace="6" width="117" height="161">There 
            is no doubt that Oman is a football-crazy nation. Almost anywhere 
            you go in the Sultanate you will find boys playing soccer, usually 
            barefoot on makeshift pitches in the sand. Clearly, football reigns 
            supreme among sports in the Sultanate. And translating raw 
            enthusiasm for the sport into a structured, profit-making enterprise 
            is something that the Sultanate is now witnessing. The Sultanate’s 
            historic advance into the final round of the GCC Cup has shown the 
            way to make domestic football a profit-making business. <br>
            <br>
            The Oman Football Association (OFA) is implementing a five-year plan 
            to develop soccer in Oman and turn it into a profitable business. 
            Football is undoubtedly the largest attraction as far as sports in 
            Oman is concerned. It is, indeed, in a better position than any 
            other sport in the country. Nevertheless, when compared to what’s 
            happening globally, the country needs to bolster its efforts to 
            build better awareness and co-operation from the private sector. <br>
            <br>
            The Omani government gives the OFA RO700,000 ($1.82 million) a year 
            in funding. However, this amount seems negligible when compared with 
            the amount being pumped to promote different types of games in other 
            countries. For example, the Spanish club Barcelona spent $8 million 
            on developing homegrown soccer talent in 2003.<br>
            <br>
            The OFA’s development plan kicked off with the establishment of 24 
            youth academies across the country that encourage local youth 
            participation in football and has more than 30 players and paid 
            staff consisting of coaches, dieticians and medics to assist the 
            youngsters to get to the top of the sport. The OFA received $1 
            million (RO384,615) funding between 1999 and 2002 from FIFA’s ‘Goal’ 
            programme to run these academies. <br>
            <br>
            In 2001, the OFA scrapped regulations governing transfers that 
            prevented nationals from leaving the country to play football 
            abroad. It also introduced measures to promote mergers between clubs 
            and has plans to increase the number of teams in the premier 
            division. Aside from youth training, the focus of the OFA’s 
            five-year plan is to develop the marketing side of the sport.<br>
            <br>
            As a result of the constant impetus received by the fame, the Oman 
            national football team made the nation proud by qualifying for the 
            Gulf Cup finals for the first time in 30 years. They left a lasting 
            impression of the Gulf Cup championship and displayed how dedication 
            and training has improved the performance of their game over the 
            years. Oman’s erstwhile coach, the Czech-born Milan Macala, was 
            granted the Order of Merit (second class) by his Majesty Sultan 
            Qaboos bin Said on the team’s brilliant performance. <br>
            In fact, such are the passions that the game engenders among the 
            citizens of the country that on the eve of the Gulf Cup finals, men, 
            women and children alike—those who could not physically make it to 
            Doha to watch the game—were glued to TV sets till the dying moments 
            of the match. Moreover, football is also being used to fulfil 
            charitable causes – the Zubair Corporation, for instance, intends to 
            use the passion the game generates to raise funds for the Asian 
            tsunami victims through an auction of sports accessories signed by 
            the national football team. This is proof enough that football as a 
            game has come of age in the Sultanate. <br>
            <br>
            <b>Restringing tennis</b><br>
            <img border="0" src="im3.jpg" align="left" hspace="5" vspace="2" width="125" height="149">Tennis 
            has only started to launch its own virtuous cycle. Globally, the 
            Grand Slam and other major tournaments do well and are attractive to 
            broadcasters and sponsors. Locally, however, long-term trends for 
            the sport’s overall TV ratings are mostly flat. <br>
            <br>
            Tennis is still one of the world’s most popular sports, but its 2002 
            global revenues from TV rights were estimated at only $200 million. 
            While local figures were not available, it can be anyone’s guess 
            that the numbers will definitely not be significant. On a global 
            scale, fragmentation is the root cause of the current inability of 
            tennis to fully translate its popularity into cash. Each of the 
            Grand Slam tournaments, which capture the lion’s share of the 
            sport’s earnings, is owned and run separately. <br>
            <br>
            Furthermore, the sport has a glut of governing bodies – ITF 
            (International Tennis Federation) handles Olympic tennis events, the 
            Davis Cup (men), and the Fed Cup (women). ATP (Association of Tennis 
            Professionals), on the other hand, runs the men’s circuit, and WTA 
            (Women’s Tennis Association) runs the women’s. Evidently, the result 
            is a year-round tennis calendar that lacks a clear start or climax 
            and is packed with tournaments going on simultaneously at disparate 
            locations throughout the globe.<br>
            <br>
            As expected, media coverage and promotional efforts are thus 
            diluted, making it hard for consumers to grasp who is playing whom, 
            who is number one in the world at any given time, and why a specific 
            match or tournament matters. Players don’t always keep their 
            commitments to appear in key tournaments.<br>
            <br>
            The diffuse calendar of events makes them less attractive to 
            consumers, and the divided ownership structure lowers the sale price 
            of commercial rights and impedes a coherent branding strategy. To 
            secure a prosperous future and to convert the sport’s popularity 
            into stronger streams of revenue, tennis organisations across the 
            world are now challenging the status quo. Some tennis executives 
            believe that they can improve their negotiating position by bundling 
            TV rights as well as streamlining the tournament calendar. The ATP 
            and WTA have taken the first steps by cooperating on operational 
            initiatives (such as education, research, administration, and joint 
            media guides), and they are considering the idea of combining 
            certain key tournaments.<br>
            <br>
            However, in Oman, the game has received a shot in the arm by the 
            emergence of young and upcoming players like Fatma Al-Nabhani, Rohan 
            Parambi and Mohammed Al Nabhani. Oman Tennis Association (OTA), 
            which celebrates its 19th anniversary on January 30, 2005, is riding 
            high on the successful organisation of the first-ever International 
            Tennis Federation’s West Asian Under-13 Championships in the 
            country.<br>
            <br>
            Oman’s best-known face in international tennis circuit, Fatma Al-Nabhani, 
            who was part of the Asian team for the Asia/Pacific Under-14 Series 
            in Australia recently, has been hitting the headlines with her 
            prowess on tennis courts in the country as well as abroad. Fatma Al-Nabhani 
            has achieved what no men have done on the tennis circuit – she has 
            won an ITF title while leaving her mark on the junior circuit. Her 
            success has also brought cheer to the game, with brands like DHL, 
            HSBC, Khimji Ramdas, KPMG, Oman Arab Bank, Omanoil Souk, Pocari 
            Sweat, Suhail Bahwan group, and Zahara Tours supporting the sport.
            <br>
            <br>
            <b>Rallying for motor sports</b><br>
            <img border="0" src="im4.jpg" align="right" hspace="5" vspace="2" width="147" height="132">F1 
            is incredibly profitable, making $810 million (RO311.54 million) a 
            year in the US alone. On the global arena, where sports fans are 
            spoilt for choice every day of the week, motor sport is finding its 
            way into people’s hearts and television screens. With one small 
            problem, though – it is perceived to be too straitjacketed a sport 
            with the least possible audience involvement. Besides, it is an 
            expensive sport too, with the car representing a significant portion 
            of a player’s budget for a season.<br>
            <br>
            Obviously, fans need enticing – mainly because Formula One is beyond 
            their comprehension. Only one man, Michael Schumacher, gets to win 
            and a lot of people around the globe don’t really know what he looks 
            like. Neither do they understand an event in which, more often than 
            not, a fifth of the field is disqualified at start! <br>
            <br>
            What motor sports needs, plain and simple, is a hero whom audiences 
            can relate to. Fortunately, Oman has just such a hero – Hamed Al 
            Wahaibi. Motor sport is pegged as one of the fastest-growing 
            spectator sports in the world. And it is definitely a sport that is, 
            slowly but surely, moving into the fast track in Oman. As the 
            industry becomes more competitive, the bigger companies are slowly 
            putting money on to the racetrack. <br>
            <br>
            <img border="0" src="im7.jpg" align="left" width="147" height="103">Oman 
            Automobile Association in conjunction with BankMuscat and Red Bull 
            organised the fourth and final round of the Oman rally championship 
            2004. On December 31, 2004, 16 contestants took part in the rally 
            that consisted of two phases, covering a total of 66.60 km. <br>
            <br>
            However, finances are a concern with even legends like Wahaibi. In a 
            recent interview to SportsOman.com, Wahaibi had said: ‘‘At the end 
            of the day, it comes down to finances. Rallying is an expensive 
            sport in which to compete and at the top levels, the amount of money 
            needed to be competitive is staggering.’’<br>
            <br>
            With regard to the finances, Wahaibi has been lucky to have the 
            support from His Majesty as well as his long-time sponsors 
            BankMuscat, Zubair Automotive and, more recently, Quwat Jabal. He is 
            hopeful that either the government or the private sector will come 
            forward to support another campaign for the World Rally Championship 
            – probably his last. <br>
            <br>
            Wahaibi, though, is one of the few who could rise above the crowd to 
            give car rallying a chance of nudging its way into the front pages 
            of local and regional newspapers between football and a report on 
            the latest news on Iraq’s move towards democracy.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Teeing off golf</b><br>
            <img border="0" src="im5.jpg" align="left" hspace="5" vspace="3" width="100" height="219">Golf 
            as a leisure activity has expanded enormously over the past 50 
            years, but only recently has the professional game begun to realise 
            its commercial potential. The sport is now enjoying the fruits of a 
            virtuous cycle. Golf is far more than just a game. It is a 
            multi-billion dollar industry, intrinsically linked to big business 
            and a key driver in many countries’ tourism sectors. Its growing 
            popularity, and the advent of superstars like Woods, has turned the 
            humble golf course into a world stage.<br>
            <br>
            The emergence of Tiger Woods, a megastar popular not only in the US 
            but throughout the world, was fortunate for golf, and the PGA was 
            quick to seize the opportunity to intensify its promotional efforts. 
            To that end, Woods and other top players are contractually obliged 
            to spend several hours during tournament weeks doing public 
            relations. This requirement exposes fans to their favourite golfers’ 
            off-course personalities—a significant way of fostering interest in 
            the tour. <br>
            <br>
            As far as Oman is concerned, golf enthusiasts still have to carry a 
            patch of Astroturf around what is effectively an 18-hole bunker. The 
            country is still to see its first green golf course. The Muscat Golf 
            and Country Club, the unique 18-hole tournament-grade course, has 
            been fashioned by top designer David Thomas to challenge the best 
            golfers, with the first round of serious golf play expected to 
            commence later this year.<br>
            <br>
            The browns, on the other hand, are designed away from the bustling 
            life of the capital city, towards Seeb International Airport. 
            Initially it was Petroleum Development Oman (PDO) that chalked out a 
            golf club and then came the Ghallah Wentworth Golf Club (GWGC), 
            Oman’s second oldest golf club. Down south, in Salalah, exists the 
            Flamingo Park, which also boast of well-laid-out browns. The Wudham 
            Naval Golf Club and Al Maha Golf Club are the other two venues where 
            golfing is at its peak during the weekends. <br>
            <br>
            The BankMuscat Club Golf Championship 2005 was recently concluded at 
            the GWGC. The two-day event showcased some of the finest golfing 
            talent in the country, and saw close to 50 golfers vie for top 
            honours. Golden boy Azzan bin Mohammad Al Romhi, the rising golf 
            sensation of Oman and son of HE Dr Mohammed Al Romhi, minister of 
            oil and gas, delivered a golden touch and walked home with the 
            overall honours of the championship. Azzan Al Romhi was also awarded 
            the “Best Player” in the 25th Arabian Golf Championships held late 
            last year after dropping the ball in the pit from a distance of 209 
            meters.<br>
            <br>
            BankMuscat has also signed an annual contract as a major sponsor of 
            the GWGC’s all-round activities. The championship this year also 
            marked the first time that BankMuscat is entering the golfing arena. 
            Confirming this move, a senior BankMuscat spokesperson said: “With 
            golf rapidly gaining popularity in Oman, we believe that it is the 
            right time to become a part of the golfing circuit.” <br>
            <br>
            <b>Hooking up cricket</b><br>
            <img border="0" src="im6.pg.jpg" align="right" hspace="5" vspace="2" width="100" height="269">Cricket 
            as a business has experienced significant growth in recent years, 
            reflected by its ability to draw large crowds, major sponsors and 
            media partners. Not only are the salaries for the world’s top 
            cricket players mind-blowing, but also the amount of money top 
            players make through endorsements of FMCG and consumer durables. 
            Insiders say Indian superstar Sachin Tendulkar is the highest-paid 
            player in all of cricket, although most of his $15 million (RO5.77 
            million) annual pay comes from endorsements. Several Australians are 
            also very well paid. Ian Harvey makes about $800,000 (RO307,692) a 
            year. <br>
            <br>
            In 2003, Australia had 25 players under contract; their minimum 
            salary was roughly $94,000 (RO36,154). Furthermore, they qualify for 
            performance-based bonuses and all share in a $11 million (RO4.23 
            million) pot allocated to player payments. That averages out to 
            about $440,000 (RO169,231) per head.<br>
            <br>
            With a sizeable expatriate population from the cricket-crazy 
            subcontinent, Oman is not immune to the cricket fever. Cricket in 
            Oman has received an impetus with the government supporting the 
            sport in the last few years. It currently boasts of 700 registered 
            players and 62 teams. <br>
            <br>
            In fact, the Oman Cricket Club (OCC) has deputed Sandeep Patil, a 
            former Indian Test and One-Day International player for coaching its 
            team. Patil recently took charge of the probables selected to 
            represent the OCC team at the International Cricket Council’s 
            Championship Trophy to be held in Ireland in June this year. <br>
            <br>
            On the other hand, His Highness Sayyid Haitham Bin Tariq Al Said, 
            patron-in-chief of the OCC, was recently accorded with the honorary 
            membership of the Cricket Club of India (CCI), which is bound to 
            open new avenues for cricket in Oman. “I have been associated with 
            the CCI for some time. It is a big honour for me. Now I have to see 
            what I can do with this opportunity,” said HH Al Said, who is also 
            Oman’s minister of Heritage and Culture.<br>
            <br>
            With Oman reaching the final of the Asian Cricket Council Trophy 
            last year and thus qualifying for the ICC Trophy to be held this 
            year, the minister hopes he can do his bit to promote the sport. “I 
            would like to further develop ties between CCI and the OCC.” With 
            cricket played in only six or seven clubs, the minister wants to 
            introduce the game to other clubs as well.<br>
            <br>
            The OCC, under the auspices of the Ministry of Sports Affairs, will 
            conduct a floodlit limited overs match in aid of the tsunami victims 
            at the Sultan Qaboos Sports Complex on February 10, 2005. The match 
            will be of 25 overs duration and will be played between the OCC ‘A’ 
            and OCC ‘B’ teams captained by HH Sayyid Haitham bin Tareq al Said 
            and Sandeep Patil, respectively. Cricket in Oman is widely supported 
            by the Khimji Ramdas group, with Pankaj Khimji, director and CEO of 
            the company, also a member of the OCC. <br>
            <br>
