                    <font face="Verdana">Underwriting Success </font></font><font face="Verdana" size="1">
                    <br>
                    <br>
                    <b>Making a foray into a market already teeming with players 
                    from the industry and jostling to find a niche amidst 
                    intense competition may not be everyone’s cup of tea, but 
                    challenges seem to energise the founders of Oman Qatar 
                    Insurance Company SAOC (OQIC). Janaki Raghavan profiles the 
                    latest entrant in the Sultanate’s insurance industry</b></font></p>

                    <p align="justify"><font face="Verdana" size="1">
                    <img border="0" src="im15.jpg" align="left" hspace="5" width="125" height="131">A 
                    joint venture between Qatar Insurance Company (QIC) and the 
                    Oman-based Saud Bahwan Group, Mustafa Sultan Enterprises and 
                    Jawad Sultan International, OQIC commenced operations in 
                    early September last year. Unfazed by competition—OQIC 
                    shares the country’s RO100 million insurance market with 12 
                    other players—the company has announced ambitious plans to 
                    capture between 3 and 5 per cent of the market by end this 
                    year. <br>
                    <br>
                    “Although apparently the Omani market is saturated, when 
                    viewed from the perspective of individual segments, some 
                    areas offer immense possibilities for growth. We just need 
                    to identify this potential and address it with suitable 
                    products and then there will be no looking back,” asserts 
                    Murtadha Jawad Ibrahim Al-Jamalani, general manager, OQIC.<br>
                    <br>
                    <b>Hitting the bull’s eye</b><br>
                    Steered by this reasoning, OQIC has made the corporate 
                    segment its focal area while it will also operate in the 
                    retail segment, which is dovetailed to the corporate 
                    segment. This makes sense considering that OQIC can 
                    strategically gain from the expertise of QIC, which has a 
                    strong presence in the corporate sector – particularly in 
                    the oil &amp; gas sector. The company has a diverse range of 
                    products in its portfolio, including property and pecuniary, 
                    liability, contractors, marine cargo, general accident and 
                    group medical, to name a few. <br>
                    <br>
                    OQIC markets and distributes its range of insurance policies 
                    through brokers appointed by clients, direct corporate 
                    clients who do not make use of intermediaries, affinity 
                    groups who arrange insurance programmes for members and 
                    industrial and commercial clients who recognise the value of 
                    first-class security and technical expertise when they 
                    arrange their insurance coverage. OQIC’s reinsurance 
                    programme is organised directly with internationally reputed 
                    re-insurers such as Swiss RE, MAPFRE, SCOR etc.<br>
                    <br>
                    While the company hopes to rope in all categories of 
                    customers in the corporate segment—financial institutions, 
                    consultancy firms, automotive organisations, restaurants, 
                    supermarkets etc.—it is particularly eyeing the oil &amp; gas 
                    and construction sectors. “Following in the footsteps of our 
                    parent QIC, which is a pioneer in the energy sector in Doha, 
                    we are hoping to make substantial inroads in this area,” 
                    claims Al-Jamalani.<br>
                    <br>
                    <b>All in the game</b><br>
                    But why did they choose to court risk entering the fragile 
                    Omani market despite the ONIC fiasco, which unnerved the 
                    industry as a whole? Al-Jamalani is categorical in stating 
                    that this move was the outcome of proper deliberation. “Oman 
                    is a planned and well regulated economy, and the mechanism 
                    functions in an organised manner. Given this backdrop, any 
                    business that is backed by sufficient investment and proper 
                    management is only bound to succeed. Of course as with any 
                    other business there is always an element of risk involved 
                    irrespective of safeguards, legal provisions etc., and we 
                    will have to brave it,’’ he reasons. <br>
                    <br>
                    ‘‘This, coupled with the fact that the country’s GDP is 
                    growing, makes Oman a promising market. Also, there is a 
                    growing awareness about saving and security, and insurance 
                    is gaining ground since it addresses both these aspects,” he 
                    adds. It is estimated that, at present, people in Oman spend 
                    on an average between $65 and $80 (RO25 and RO30) annually 
                    on insurance as compared to countries like Japan, where the 
                    annual insurance spend per person is believed to be about 80 
                    times more than this! The insurance spend in Oman is mainly 
                    directed to auto insurance, which translates into a huge 
                    potential in other areas.<br>
                    <br>
                    <b>Committed to core values</b><br>
                    The company’s results in the last 5 months vindicate the 
                    fact that it is focused on fundamentals. In the words of Al-Jamalani, 
                    “Given the fact that we do not compromise or compete on risk 
                    management, business has been beyond our expectations.” He 
                    attributes the success to the company’s unstinting 
                    commitment to its corporate philosophy. <br>
                    <br>
                    “Central to OQIC’s operative agenda is customer focus, 
                    wherein we make an all-out attempt to meet customer needs 
                    and expectations by offering innovative products and the 
                    highest service standards. As a company driven by 
                    strategies, we also build on our competitive advantages to 
                    improve our shareholders value,” he says. <br>
                    Staying competitively and financially focused as well as 
                    being cost-conscious and pressing its technological 
                    expertise to maintain competitive advantage are the other 
                    focal areas of the company’s agenda.<br>
                    <br>
                    <b>Advantage OQIC</b><br>
                    “Since its establishment in 1964, QIC has acquired 
                    experience in every major economic sphere in the region, 
                    helping it to acquire regional contacts, which gives it an 
                    edge over foreign insurers and other carriers. We also have 
                    an efficient GCC network with offices in Doha, Kuwait, 
                    Saudi, Abu Dhabi and Dubai, which works in our favour,” says 
                    Al-Jamalani. That QIC has a ‘A-’ rating from Standard &amp; 
                    Poors and an ISO 9001:2000 certification also adds 
                    substantially to OQIC’s brand equity.<br>
                    <br>
                    As with all professionally managed companies, OQIC lays due 
                    emphasis on HR, as is evidenced by its professional team. 
                    Harnessing local talent has been on top of its agenda, and 
                    Omanis comprise 50 per cent of OQIC’s staff as against the 
                    statutory requirement of 45 per cent for the industry. <br>
                    <br>
                    <b>Future forward</b><br>
                    “There is a misguided notion that insurance companies form a 
                    cartel and flex their muscles to control prices. Society 
                    should wake up to the fact that if it has to benefit, 
                    insurance companies should be strong – which means that they 
                    have to charge the right prices. If the government tightens 
                    measures and modifies certain laws to meet privatisation 
                    needs including provisions regarding workmen compensation, 
                    professional indemnity, making general third party liability 
                    compulsory etc., they will succeed in instilling greater 
                    confidence in the public and the industry will witness a 
                    growth curve,” believes Al-Jamalani.<br>
                    <br>
                    Although Al-Jamalani claims that OQIC will introduce new 
                    products in phases, he keeps the details guarded. “We will 
                    be launching some products by the end of this year targeted 
                    at certain market segments with a prudent cost to benefit 
                    ratio. <br>
                    <br>
                    We do look forward to governmental support for these 
                    products.” Other plans include applying for a rating once 
                    the company has achieved sizeable business and going in for 
                    the ISO Certification.</font></p>

