and documentation. The new model also has an advanced VGA camera, and 65K colour 
display.<br>
<br>
The compact messaging device supports a wide range of e-mail and instant 
messaging solutions, easy-to-use Personal Information Management (PIM) 
capabilities and wireless data synchronization. The company plans to make the 
Nokia 6822 available in the first quarter of 2005 in EMEA.<br>
<br>
Users of the Nokia 6822 benefit from carrying a single device that is versatile 
enough for both personal and business use. The compact device supports multiple 
email accounts and calendars and can be synchronized from device to PC with a 
few steps. Configuration is easy for email, messaging or Internet browsing via 
over-the-air settings available from most network operators. The phone also 
includes pre-installed mobile applications for private and business use.<br>
<br>
“People love the full-keyboard design and functionality of our messaging 
devices,” said Niklas Savander, senior vice-president of Nokia’s business device 
unit. “To date, more than two million people have adopted this popular format. 
As mobile instant messaging is gaining popularity worldwide as a quick and 
efficient conversation tool, and we expect demand for these devices to increase. 
The Nokia 6822 is a great phone and a great device for messaging at work and at 
play.”<br>
<br>
“Mobile messaging and push email continue to represent applications that will 
provide the greatest purpose and value for a growing class of mobile users,” 
said Andrew Brown, Program Manager for European Mobile Devices at IDC. “The 
Nokia 6822 represents a continuation of Nokia’s line of highly innovative flip 
open full-messaging keyboard devices, that will serve a user’s needs for both 
personal and corporate email as well as offering the latest in mobile phone 
technology.”</font></p>

