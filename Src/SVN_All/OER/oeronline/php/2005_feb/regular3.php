</b>Last month, the Ministry of Education in the 
Sultanate of
Oman announced the completion of the first stage of its major
infrastructure upgrade project. The project, which uses
Microsoft Windows Server Systems solutions, will create the
foundation for the state-of-the-art infrastructure that will
eventually connect the Ministry and its staff with every public
school and teacher in the Sultanate.
<br>
<br>
“As part of our goal of providing the highest standards of
education, the Ministry has embarked on this project to connect
all of our staff and teachers across Oman. For such a major project,
we required not only a superior technology platform, but
also a committed partner. Microsoft’s commitment to Oman,
through its local presence, and its proven commitment to the
use of ICT in education through the Partners in Learning
Program, convinced us that Microsoft was the best partner for
this,” said Khalid Al-Siyabi, IT Director, Ministry of Education.
<br>
<br>
The Ministry’s IT department, working with personnel
from Microsoft Oman, deployed Active Directory based on
Microsoft Windows 2003 at the Muscat headquarters, and are
now ready to extend the Directory Service to regional offices
across the Sultanate. The Windows Server System, with
Windows 2003 as the foundation will provide the Ministry
with an integrated server infrastructure designed to help IT
staff to deliver new capabilities and support, while simplifying
and optimising IT infrastructure.
<br>
<br>
The initial phase of the project involved the migration of
the Ministry’s servers from Windows NT to Windows 2003.
Deployment of Windows 2003 took just two months, and the
Ministry has also deployed other elements of the Microsoft
Windows Server System like the Management Suite, Microsoft
Systems Management Server 2003 and Microsoft Operations
Manager which will provide IT staff with powerful tools to
manage a wide range of network administration and management
tasks. For internal communication across the entire staff,
instant messaging is available through the deployment of
Microsoft Live Communication Server. The communications
capabilities will be completed with the roll out of Exchange
2003, which is due to begin in the near future.
<br>
<br>
Over 900 staff in the capital access the Ministry’s network,
and one of the major elements of the project was replacing the
Windows NT directory with Windows Active Directory, to
manage user identities and network structure. The network
will be extended to another 2,000 of the Ministry’s staff that
work across eleven regions in Oman, with the eventual aim of
providing communications for 55,000 teachers in all 1,200
public schools in the Sultanate.<br>
<br>
“One of the biggest reasons that the Ministry decided to go
with Microsoft software was the presence of local support
through Microsoft Oman,” explained Arif Suri, IT Consultant,
Ministry of Education. “The fact that Microsoft is present in
Oman, with a strong team, gives us real confidence. Our IT
staff are also comfortable with Microsoft solutions, as most of
them are Microsoft Certified Systems Engineers, so Windows
Server System is very convenient both in terms of functionality
and features and in terms of maintaining and administering
the network.”</font></p>

