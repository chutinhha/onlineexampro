<p align="left"><font face="Verdana" size="4" color="#FF0000">A non-zero sum 
game</font><br>
<br>
<font face="Verdana" size="1"><b>Valeed Ahmad Ansari explains why outsourcing is 
not only important, but also vital for the lifeline of the US economy</b></font></p>

<p align="justify"><font face="Verdana" size="1">The subject of outsourcing has 
received a lot of attention during last few months. It has attracted tremendous 
coverage in the media and was one of the hottest issues in the US presidential 
election. The issue is not confined to US alone, but also has received worldwide 
attention.<br>
<br>
<b>What is outsourcing?</b><br>
The term outsourcing is defined in Merriam Webster dictionary as “the practice 
of subcontracting to outside and especially foreign or non-union companies”. <br>
<br>
The term ‘outside’ here means outside the firm or outside the country. Thus 
outsourcing can be domestic or international. Example of domestic outsourcing 
would include, say, a Muscat-based firm contract out a Salalah-based firm to 
manufacture some of the components or parts for its final product. International 
outsourcing (also called offshore outsourcing or simply offshoring) can be 
defined as the procuring of service or material inputs by a firm from a source 
in a foreign country. <br>
<br>
In the early 1980s, outsourcing typically referred to a situation when the firm 
expanded its purchase of manufactured physical inputs to an outside provider 
rather than making them in-house. The recent advances in computing, 
communication and information technology have made the outsourcing of services a 
practical possibility that was not possible in the past. <br>
<br>
What is novel now is that outsourcing has extended to specific segments of 
international trade consisting of arm’s length long distance purchase of 
services abroad, mainly but not necessarily via electronic medium such as 
telephone, fax and the Internet. This type of outsourcing occurs when a phone 
call center located in Bangalore serves customer in Washington or a digitally 
transmitted X-Ray is read in Mumbai. This genre of arm’s length outsourcing is 
what has created furor.<br>
<br>
<b>The debate on outsourcing </b><br>
The public debate in the US on outsourcing was ignited by Gregory Mankiw, head 
of White House council of Economic advisors and Harvard’s youngest tenured 
professor, while releasing the annual economic report of the president, he said 
outsourcing is a new way of doing international trade and it is probably a good 
thing for US jobs in the long run. In a press interview, he observed: “We are 
very used to goods being produced abroad and being shipped here on ships or 
planes. What we are not used to is service being produced abroad and being sent 
here over the Internet or telephone wires.” <br>
<br>
The President’s report itself offered encouraging words about outsourcing. Soon, 
it was denounced as a conspiracy to ship jobs abroad and caused a firestorm of a 
debate. What Gregory Mankiw was trying to enunciate was that outsourcing 
fundamentally is a trade phenomena and its effects are akin to international 
trade in manufactured goods. Precisely put, he was restating economic law of 
comparative advantage for the 21st century propounded in 1814 by David Ricardo.<br>
<br>
<b>Principles of comparative advantage</b><br>
According to the principles of comparative advantage, the gains from trade 
follow from allowing an economy to specialise. If a country is relatively better 
at making textiles than computer chips, it makes sense to put more resources 
into textiles and export some of the textiles to pay for imports of computer 
chips. This is true even if that country is the world’s best computer chip 
producer since the country will have more of both textiles and computer chips 
than it would have without trade. In the world of comparative advantage theory, 
it is relative advantage that matters. The current trend of outsourcing business 
processes is a manifestation of comparative advantage at work.<br>
<br>
<b>Benefits and costs of outsourcing</b><br>
Economists argue that outsourcing is another form of international trade, which 
creates wealth in the economy. They point out that employing workers at a lower 
cost enables US companies to be efficient and productive, permitting them to 
create the same amount of goods with fewer resources or more goods with the same 
resources. <br>
<br>
In turn, this reduces the price of goods in the US, strengthens US companies and 
releases workers for other tasks. The saving obtained from outsourcing allows 
companies to survive and expand in highly competitive markets.<br>
<br>
McKinsey and Company estimated that net cost saving accruing to US companies 
from some of the offshored job is about 50 per cent. It leads to lower inflation 
and higher productivity to conduct a more accommodative monetary policy and 
creating and sustaining conditions for higher employment. <br>
<br>
According to one estimate, without foreign outsourcing, GDP growth of US economy 
would have been lower by 0.3 per cent a year between 1995 and 2002.<br>
<br>
Further, offshoring also increases demand for American products and services. 
The companies that provide offshore services require goods and services 
themselves ranging from computer to telecommunication equipments to legal, 
financial, and marketing expertise and buy these from US companies. <br>
<br>
On the flip side of outsourcing, the most frequently cited estimate is due to a 
report from Forrester Research Inc., according to which the total number of US 
jobs outsourced will reach 3.3 million—recently revised to 3.4 million—by 2015. 
A recent study by Ashok Bardhan and Cynthia A. Knoll at the University of 
California suggests that up to 14 million Americans now work in occupations – 
including financial analysts, medical technicians, paralegals and computer and 
math professional that could reasonably be considered ‘‘at risk’’. <br>
<br>
According to Economist magazine, the US economy destroys and creates a huge 
number of jobs every month – their estimate was 2 million or more. In 
Shumpterian language, this churning is called ‘capitalist creative destruction’. 
The process allocates resources—money and people—to where they can be more 
productive. A vibrant dynamic economy like the US will bundle activities into 
increasingly higher value-added activities. For example, although 70,000 
computer programmers lost their job during 1999-2000, more than 115,000 computer 
software engineers found higher paying jobs during the same period. <br>
<br>
Outsourcing is only a tiny phenomenon in the US labour market. Some 70 per cent 
of the US economy is not vulnerable to offshoring because it is in industries 
such as retail, restaurants and hotels, health care and other services that 
cannot be moved abroad. Moreover, most economic studies cast doubt on trade 
being the primary cause of job losses and gains. <br>
<br>
Another perspective is that any discussion on outsourcing should not ignore ’insourcing’. 
Just as US firms outsource positions to developing countries, firms in other 
countries outsource positions in the United States. According to the Bureau of 
labour statistics, the number of outsourced jobs increased from 6.5 million in 
1983 to 10 million in 2000. The number of ‘Insourced’ jobs increased even in the 
same period from 2.5 million to 6.5 million.<br>
<br>
This is not to say that outsourcing is only a plus for the economy. Even the 
unflinching free trade advocates acknowledge that outsourcing has painful and 
destabilising consequences for hundreds of thousands of workers who found 
themselves laid off. Their recommendations tend to centre on helping workers 
find new jobs and adjust to the shifting labour market via expanded training and 
relocation assistance. Many analysts calls for improved unemployment benefits 
and health insurance to help workers. There are extreme voices also who call for 
outlawing outsourcing and using tax deterrents.<br>
<br>
<b>Current Debate between Samuelson and Bhagwati</b><br>
The current debate about outsourcing got fillip when the Nobel laureate and 
America’s most celebrated economist Paul Samuelson entered into the debate. At 
89, his armour is still full of intellectual arrows. He published an article in 
the Journal of Economic Perspective to “put the record straight”. <br>
<br>
According to Samuelson, laws of economics dictate that the American economy will 
benefit from all form of international trade including outsourcing abroad of 
call centre and software programming. <br>
<br>
Samuelson calls this ‘popular polemical untruth’ and states that trade may not 
always work to the advantage of American economy. According to Samuelson, a low 
wage nation that is rapidly improving its technology, like India or China, has 
the potential to change the terms of trade with America in fields like call-centres 
or computer programming in ways that reduce growth rate of US economy.<br>
<br>
Jagdish Bhagwati, former student of Samuelson and one of the foremost 
authorities on international trade whose contribution in the field of 
international trade is so monumental that Samuelson once wrote about him that we 
are living in Bhagwati era, has along with T.N. Srinivasan of Yale and Arvind 
Panagariya of Columbia University submitted a paper in the Journal of Economic 
perspective in response to Samuelson’s article. <br>
<br>
This paper is going to be published in fall issue of the journal. Bhagwati and 
his co-authors write that the outsourcing worries are greatly exaggerated and 
false. <br>
<br>
They contend that such an assessment of education system of India and China 
“almost borders on the ludicrous”. According to Bhagwati, having a lot people 
does not mean that they are also qualified. They argue that the Samuelson model 
yields net economic loss only when foreign nations are closing the innovation 
gap but US being reasonably flexible, dynamic, innovative society can change the 
terms of trade by moving up the technology ladder and by making increased 
investment in science, research and education.<br>
<br>
It seems that the false alarm on offshore outsourcing springs more from 
psychology (read fear and anxiety) than from economics as people feel that their 
jobs are threatened. According to Bhagwati and co-authors, “Fear as the Russian 
proverb says has big eyes. It can also have deaf ears”. Stopping the juggernaut 
of outsourcing will handicap the US companies in global market place and will 
cause huge disadvantage for them.</font></p>
