<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Customer 
Service Excellence<br>
</font></font><font face="Verdana" size="1"><br>
<b>
<img border="0" src="im17.jpg" align="left" hspace="5" width="110" height="128">Ali 
Hassan Moosa evaluates the importance of excellence in customer service and 
provides a roadmap for the local institutions to follow<br>
</b><br>
Customer service is now the strategy for improving market share, retaining 
customers, and expanding business. Having a good customer service approach based 
on a customer satisfaction strategy requires the commitment of senior 
management, back-office, and front-line employees to create strong corporate 
philosophy of serving customers. <br>
<br>
Customer service is often reactive and not based on understanding a customer’s 
true needs. Banks must capitalise on what they know about their existing 
customers and continuously build on that knowledge to serve better. This method 
is often referred to as a customer-centric approach.<br>
<br>
To simulate customer experience, banks need to appoint customer champions since 
customer-centric approach cannot be departmentalised. It must originate from the 
very heart of the organisation. All departments, front and back office, need to 
work in harmony to enrich the customer experience. The champion takes the 
ownership of customer experience, communication, and relationship development.<br>
<br>
The highly acclaimed customer service <br>
and management guru, Ron Kaufman, has identified eight proven principles that he 
calls The Secret of Superior Service:<br>
1. Understand how your customers' expectations are rising and changing over 
time.<br>
2. Use quality service to differentiate your business from your competition.<br>
3. Set and achieve high service standards.<br>
4. Learn to manage your customer's expectations.<br>
5. Bounce back with effective service recovery.<br>
6. Appreciate your complaining customers.<br>
7. Take personal responsibility.<br>
8. See the world from your customers’ point of view.<br>
<br>
For more information, please log on to <a href="http://www.ronkaufman.com">
www.ronkaufman.com</a><br>
<br>
If any of the banks conducted a customer survey relationship that specifically 
asks customers to put in ascending order the most important relationship to the 
least, which would you guess would be towards the bottom end of the list? 
Probably, banks and insurers! It is also very likely that local grocers would be 
placed towards the top-end of the relationship hierarchy.<br>
<br>
Customer satisfaction surveys not only measure the degree of customer 
satisfaction but also provide banks with an insight into customer behaviour. 
Customer satisfaction surveys must be a continuous and evolving process and need 
not only be applied when things go bad.<br>
<br>
It is absolutely vital for banks, through customer satisfaction surveys, to 
evaluate customer expectations. Customers now have access to products and 
services from a large number of local and international financial 
intermediaries, and they compare price, product features, and services across 
multiple institutions.<br>
<br>
It is equally important for banks to ensure that their tactical actions are in 
sync with their strategic intentions. One way of identifying this is through 
internal service surveys. The most important objective for banks is to ensure 
continuous improvement in the organisational culture for managing customer 
expectations.<br>
<br>
Levels of service, as per customer expectations, can be categorised as; 
unbelievably bad, basic, expected, desired, unexpected, unbelievably excellent.<br>
<br>
Banks need to tailor their customer opening hours to suit customer lifestyles, 
days of the week, and the locality in which the branch operates. Some banks in 
Oman have already adopted a flexible customer hour regime to suit their customer 
needs. <br>
<br>
The competitive advantage for non-traditional financial institutions and new 
entrants into the retail market is the flexibility to tailor-make products and 
offer better services, and that is the biggest challenge that large retail banks 
face today. <br>
<br>
How complaints are dealt with, what complaint resolution methods are undertaken 
and the volume of complaints do act as an indicator of how well (or badly) an 
organisation is doing in the pursuit of service excellence. <br>
<br>
When things go wrong and a complaint is logged, the organisation needs to show 
empathy and act swiftly. A complainer who ends as a satisfied customer because 
of the way the complaint was dealt with would normally be a more loyal customer 
than s/he was before the complaint.<br>
Effective complaint management can turn into a positive advantage to the bank. 
Complaints serve to give an organisation a learning capability, and a chance to 
go that extra-mile for their customers.<br>
<br>
An intelligent way to reduce complaints, arguably, is to predict service 
short-falls – which is not easy. The process, however, is worth exploring in 
order to ensure that the level of complaints is not overwhelming.<br>
<br>
Historically, banks had measured the success of customer relationships through 
fee generation. The process named ‘customer profitability’ had the objective of 
segregating profitable customers from non-profitable ones.<br>
<br>
So, what should banks do with unprofitable customers? They certainly need to 
look into the cost of retention against the cost of rejection. Rejected 
customers would obviously go to competitors. <br>
<br>
It is dangerous to the life-line of the company if most rejected customers move 
into a single competitor who may as a result be able to create critical mass and 
implement economies of scale that makes such customers a valuable resource to 
them, and thus, creating a competitive edge over you.<br>
<br>
So, the very fact that customers may not be valuable in your sphere does not 
always mean they are worthless. It may be worth retaining unprofitable customers 
to ensure that competitors are not in a position to exceed your levels of 
service. <br>
<br>
The new customer generation measures its their bank relationships based on the 
bank’s ability to generate and maintain its wealth. Customers seem to hold their 
banks accountable for their ability to enhance the quality of their lifestyle. 
This clearly is a contrary view to the earlier one. <br>
<br>
Through the application of customer intimacy, financial discipline, and 
technology, banks can apply the concept of wealth management and personal 
finance to all customers and not only the so-called ‘High Net Worth’. <br>
<br>
One of the leading banks in this field is the global banking giant HSBC, who has 
introduced personal finance into different retail markets. The change process 
(as always) is long and painful, but it is my strong belief that personal 
finance management coupled with customer intimacy is the (only) way forward for 
retail banks.<br>
<br>
To be fair, banks in Oman have, indeed, tried to take a customer-service 
approach in the past but haven’t been as successful in it as they would have 
liked to be. <br>
<br>
What happens when you provide a consistent, excellent customer service over a 
relatively long period of time? Customers will perceive the level of service as 
‘ordinary’. To avoid great service becoming the norm, it is important to make 
sure that your mission statement reflects precise service standards. Should you 
continuously exceed the stated level of service, then the organisation is well 
underway to creating the ‘wow’ factor in customer experience.<br>
<br>
The ability to tailor services to different customers, ethnic groups, 
lifestyles, life stages, and other means of personalising banking products will 
clearly meet the growing demand of the new generation customers. <br>
<br>
Empowering front-end staff to customise services backed with operational 
excellence should project the branch staff as the trusted advisor for their 
finances, and thus, moving closer towards achieving customer service excellence.</font></p>

