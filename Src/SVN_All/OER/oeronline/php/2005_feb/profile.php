<br>
<b>Rafia Al Talei profiles the buzzing persona of His Excellency Shaikh Mohammed 
Bin Abdullah Al Harthy, the Minister of Transportation and Communication</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im8.jpg" align="left" hspace="5" width="150" height="190">Sitting 
in his elegant Arabesque office overlooking the Gulf of Oman, His Excellency 
Shaikh Mohammed Bin Abdullah Al Harthy is one of the young faces in the Omani 
cabinet. A much-experienced man, the minister has occupied a number of 
government posts. He was a financial researcher in the Ministry of Finance. 
Later, he held different posts like Loan Administration Director and Planning 
Director in the same Ministry, after which he became the Undersecretary for the 
Ministry of Transportation and Communication and then the Minister of 
Transportation and Communication. <br>
<br>
<b>Childhood memories</b><br>
His Excellency Al Harthy was born in Muscat but brought up in Qabil. He is the 
eldest of seven brothers and sisters. During his childhood, he moved to 
different regions in the Sultanate and studied in different schools. A learned 
man, HE Al Harthy has a B.S. in Economy as well as a B.S. in International 
Management. He finished his postgraduate studies in political sciences and 
graduated from New England University, England. His father was a wali (governor) 
before retirement. <br>
<br>
<b>Management style </b><br>
Running a busy and active Ministry is not an easy task. To keep things in 
control, HE Al Harthy follows a varied system of management. “I do not follow a 
single system of management, but a variety of management techniques. I choose 
the system that is best suited for a given situation. To focus on implementing 
nationwide aspirations and general plans, I usually leave the daily 
administrative log of work to a number of trustworthy and skilled Omani 
executives and administrators”, emphasises HE Al Harthy. <br>
<br>
“Work in the Ministry of Transportation and Communication requires great 
concentration and huge efforts as its activities are connected with a number of 
other governmental bodies like the Communication Organising Authority, Omantel 
and Tender Council. It is due to this that I assign administrative work to my 
aides and executives. I feel such a policy has worked well,” he adds. <br>
<br>
<b>An undaunted persona</b><br>
HE Al Harthy is a man who knows how to face adverse conditions. “Difficulties 
and challenges are a part of life. They only make you stronger. Negotiations for 
the Manah power station were one of the most difficult challenges I have ever 
faced. I was representing the Ministry of Finance then, and was negotiating to 
carry out a first of its kind project in the Middle East. I started from ground 
zero because nobody had previous experience in this field. The case is different 
in the Ministry of Transportation and Communication. I may be lucky, as I have 
not faced much difficulty till now. We are working hard to satisfy the growing 
needs of the industrial sector,” says the Minister. <br>
<br>
<b>Views on privatisation</b><br>
“The Sultanate enjoys a large geographical area of land. The case in Oman is 
that the more you add to the service, the more your services come in demand. The 
government alone cannot satisfy the growing needs of all sectors. For this 
reason, we privatised some projects that are attractive to local and foreign 
investors and retained still others that might not have been too appealing to 
the private players at this stage,” says HE Al Harthy. “The private sector can 
provide considerable human resources as it offers many incentives and 
remunerations to local and foreign recruits. Moreover, the rule that governs the 
running of business in private sector is more flexible than that of the public 
sector,” he added. <br>
<br>
<b>Self-management of the aviation sector</b><br>
Commenting on the failure of reaching an agreement with the company that has 
been operating the Al Seeb airport, the minister said: “We did not reach a 
compromise. During the guarantee period, we started development process in the 
airport and the construction of a new building. According to the agreement 
signed with the British company, they were supposed to develop the existing 
airport building. But now we have taken concrete steps to construct a new 
building in the northern area of the airport alongside the marine road. The new 
building will absorb 6 million additional passengers a year at least.” <br>
<br>
<b>Promoter of Omanisation</b> <br>
Omanisation process should be promoted at all levels and HE Al Harthy is a firm 
believer in it. “We have made sincere efforts to appoint our trained staff in 
different posts within the Ministry. The rate of Omanisation in the 
non-technical field amounts to 98 per cent. The rate in technical jobs is 
undoubtedly less than this. We are leaving no stone unturned to raise such rate 
every year,” commented the minister on the rate of Omanisation.<br>
<br>
<b>Reducing Oman’s dependence on oil</b><br>
Much has been talked about diversifying Oman’s source of income and decreasing 
its dependence on oil. Commenting on the issue, the minister said: “This matter 
is a bit complicated. Every country should do everything possible to set up 
infrastructure that can attract foreign investors and encourage local investors 
to invest money in their own country.’’<br>
<br>
‘‘To encourage investment, the Ministry of Transportation and Communication 
implements a number of projects like the Salalah port, which will be supported 
by a free trade area. This will make Salalah a trade centre for renowned 
manufacturers and traders. Undoubtedly, this project will attract international 
firms and encourage them to start new projects in the area,’’ said HE Al Harthy.<br>
<br>
‘‘The development of Mina Raysut into a world-class seaport is an event of major 
economic importance for the Sultanate. Oman is now poised to become a major 
player in the global shipping trade. Equipped with the most modern facilities, 
Mina Raysut will figure among the largest trans-shipment ports in the world. It 
will function as the hub of cargo relay from the Middle East and East Africa to 
the main East-West ocean routes. This will help the freight business to expand 
substantially, not only within the Middle East, but also in Europe and Asia,” HE 
added. <br>
<br>
‘‘Port Raysut will also serve as a focal point for both regional and external 
trade, besides industrialisation and creating increased job opportunities in 
Oman. The expansion of the Salalah port, which is close to major international 
container traffic route between Europe and the Far East, is aimed at making Oman 
an international re-export centre. Moreover, we have Al Dukm port for vessels 
repair that absorbs 400 tonnes of vessels. The port is rich in fish wealth. This 
will give boost to the fishery industry.’’ <br>
<br>
As for the Sohar port, it is worth mentioning that an associated free-trade zone 
will cover 47 sq km and port construction includes the creation of two 
breakwaters and dredging for the port and port entrance. It also includes cargo 
and container berths and two liquid cargo berths, to be used for oil. ‘‘The port 
is a major step in diversifying sources of income. The work is underway in Sohar 
refinery. The berths in Khasab port in Musandam will be enhanced and more 
warehouses will be added to the existing ones. All such infrastructure projects 
contribute a lot to the development of our national economy and diversifying 
sources of income,’’ HE Al Harthy revealed. <br>
<br>
<b>HR comes first </b><br>
HE Al Harthy believes that, in today’s age, HR is an investment for the future. 
“We deeply believe that a good foundation of the coming generations will be the 
basis of any future development process. The Ministry of Manpower was created 
for this purpose. The Ministry’s role is not just limited to creating jobs, but 
preparing young people to compete in the job market. Higher education officials 
are making strenuous efforts to equip the youth with science and modern 
technology, assets for any future development”, says the minister.<br>
<br>
<b>Holidaying style </b><br>
Being a minister does not give one the privilege of holidaying in far away 
places. “Before becoming a minister, I used to travel a lot, mostly to Europe 
and other destinations. Now that I’m the minister, I do not have the luxury for 
going on holidays. Therefore, I try to mix my vacation and official tours and 
work. Sometimes, I spend weekends in inspection tours,” he says. <br>
<br>
<b>A great surfer</b><br>
“I like browsing the Internet and reading, especially biographies as they give 
the reader an idea about the different aspects of the writer’s life. Reading is 
an asset to anyone interested in getting lessons on life as they talk about 
somebody’s experience”, the minister shares.</font></p>

