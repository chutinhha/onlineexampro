</font><b><font face="Verdana" size="4">Dump Dollar<br>
</font><font face="Verdana" size="1">By Michael Preiss</font></b></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<img border="0" src="cover4.jpg" width="98" height="120" align="left" hspace="6" vspace="4">My reading of the situation is that it is a question of when, not if, Gulf Arab 
oil exporting will abandon the US Dollar peg. The peg has outlived its 
usefulness even as Washington is still exerting strong political pressure to 
maintain the status quo. <br>
<br>Kuwait has so far been the most foresighted and clever GCC nation to have moved 
beyond the peg. But, Kuwait is a smaller, less strategically important country. 
If Saudi Arabia would suggest a decoupling, the Washington lobby and policy 
makers most probably would get rather concerned. Our friends in the UAE also 
realise that the US Dollar and hence AED weakness has some blessings. How does 
all this fit Dubai property, let the AED fall in line with the US Dollar and 
Dubai and UAE property will be “very” cheap for British and European buyers. In 
the case of Saudi and UAE, it hence might take at least 2-3 years until they 
scrap the currency peg.<br><br><b>External Pressure<br></b>Fear of the unknown and, of course, implied and real political pressure from 
Washington. One reason why the Arab nations are holding back is because they 
cannot agree among themselves. Somehow, there seems to be a sense that Saudi 
Arabia might dominate the others. Similar to the then displaced belief in Europe 
before the Euro was introduced that Germany might exert too strong a dominating 
role. Consensus building and the solution of putting a Frenchman at the head of 
the ECB in Frankfurt was the solution. Why not suggest a Saudi as the Head of 
the GCC Central Bank?<br><br><b>Future of Dollar Parity</b><br>Kuwait most probably will never revert back to a dollar parity since it does not 
make economic sense. Most probably, we will continue to see extended and 
prolonged US Dollar weakness. The advantage for the GCC and its people will be 
increased purchasing power and the disadvantage will be for the US and Americans 
who will have to pay much more for oil and gas in “real” terms. This could 
reduce standard of living in America as well as lead to lower economic growth in 
the years to come. So some people arguing oil at US$120 per barrel or even 
US$150 per barrel sounds unrealistic. Well, in real terms, US$120 to US$150 will 
be worth much less then it used to be. Oil in terms of Euro, Indian Rupee and or 
Chinese Yuan against this might not be that expensive in “real” purchasing power 
terms. <br><br>Will the US allow this to happen? Good question. After some time, the US will 
find that it has few options to prevent de-pegging.<br><br><i>Michael Preiss, Associate Director, Investment Advisory Group, HSBC Private 
Bank, Dubai</i></font></p>
