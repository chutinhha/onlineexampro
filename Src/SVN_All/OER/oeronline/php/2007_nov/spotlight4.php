<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">“Insurance sector is poised to grow in 
Oman”</font></b><font face="Verdana" size="1"><br>
<b>Dr J. Retnakumar, Resident Manager, New India Assurance Company Ltd, talks 
about the growth of the company as well as the insurance market in Oman</b><br>
<br>
<b>
<img border="0" src="spot.jpg" width="175" height="193" align="left" hspace="8" vspace="4">How has been the performance of New India Assurance in Oman for the last one 
year?<br>
</b>The overall performance was very good. We were the first foreign insurance 
company to enter Oman in 1974 and today we are the leading foreign insurance 
company here. As regards the operational results are concerned, we could achieve 
the highest ever increase of 33 percent in the last financial year. In the past 
we have made conscious efforts to plough back surplus in upgrading our services 
by offering new products, package products, as well as upgrading the technology 
to ensure fullest client satisfaction.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<b>What new products you are going to launch in the near future?<br>
</b>We will be introducing new schemes and products that will meet exact 
requirements of the customers. Our products introduced during the last one year 
were a huge success. Comprehensive Personal Accident Security Scheme (COMPAS), 
mainly formulated for Indian expats belonging to poorer section was widely 
appreciated by various social organisations and authorities. This policy, at a 
meagre premium, takes care of the air ticket expenses in case of evacuation, 
with Indian Embassy’s certification, accident hospitalisation, death, permanent 
disability etc. Another cover named ‘Better Health’ – a healthcare insurance 
programme as a value addition cover for various corporate groups – was launched 
recently. This policy takes care of corporate employees’ treatment in India, 
Oman, Bangladesh, Pakistan and Sri Lanka. We will be shortly introducing similar 
need-based insurance covers for the benefit of expatriates working in the 
Sultanate. However, it is too early to elaborate on those products, now.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<b>How do you see the overall insurance sector and its growth in Oman?</b><br>
The ongoing industrialisation drive in the Sultanate will give more 
opportunities for the project insurance and other related general insurance. 
Looking at the growth in the industrial and commercial sectors, insurance sector 
is poised to grow further in future. We also wish to make the most of it by 
providing excellent services with professionalism, technical expertise and 
personal touch. We will also give main focus to expand our Personal Line 
products as well as doing maximum project insurances. </font>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b>What are the major challenges and strengths of Oman’s insurance market?</b><br>
The major challenge is the unhealthy competition prevailing in this industry 
which has to be eradicated with a consensus of all the players in the market. 
The major strength of insurance market is the most efficient and impeccable 
regulatory body – Capital Market Authority – under the commendable leadership of 
HE Yahya Bin Said Al Jabri, the Executive President.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<b>What are the major challenges of New India Assurance in Oman?</b><br>
Unhealthy competition prevailing in this industry has to be eradicated with a 
consensus of all the players in the market. Though ‘New India’ in particular is 
not facing any challenge, it is the whole industry that is facing the dilemma 
and I am sure concerned authorities are definitely doing their best to curb it.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<b>How do you differentiate your company from your competitors in Oman?</b><br>
New India’s age, image and experience distinguish us from rest of the insurance 
companies not only in the local market, but globally too. Today, we have offices 
in 27 countries in all the five continents. Apart from providing insurance 
covers, we also conduct awareness programmes and workshops from time to time on 
insurance-related subjects for the benefit of general public. Since last year, 
the company has been conducting awareness programmes on road safety. ‘Drive 
Safe, Save Life’ – a seminar on safe and defensive driving, was conducted in 
association with Royal Oman Police (ROP) at Traffic Safety Institute of ROP at 
Seeb on August 28, 2006, which was the first of its kind in Oman undertaken by 
any insurance company. This year also we are in the process of conducting an 
essay writing competition for the students of English and Arabic schools in Oman 
in various categories. The competition has just been over wherein students from 
more than 30 schools in Oman penned their views. The response from schools and 
students was overwhelming and more than 500 entries were received. Twelve 
winners from various categories will be felicitated with awards and gifts at a 
glittering function that will be held on October 31, 2007 at Indian School, Al 
Ghubra. </font>
