</font><b><font face="Verdana" size="4">Gatorade Tiger in March</font></b><font face="Verdana" size="1"><br>
<b>Tiger Woods has inked a deal to have his own sports drink in a licensing 
arrangement with Gatorade<br>
</b><br>
<img border="0" src="golf.jpg" align="left" hspace="10" vspace="4">The Gatorade Company, a division of PepsiCo, and Tiger Woods have signed a deal 
to develop Tiger-inspired sports performance beverages for athletes at all 
levels. Terms of the deal were not disclosed, although Golfweek magazine 
reported that it was for five years and could pay Woods as much as US$100 
million, moving him closer to the US$1 billion mark in career endorsements. 
Woods has endorsed everything from sports equipment and apparel (Nike) to 
financial services (Accenture) to automobiles (Buick) to shaving products 
(Gillette). His first beverage deal comes after 11 years as a professional.<br>
<br>
“Gatorade has been part of my game plan for years, whether I’m training or 
competing, so this is an ideal match,” said Woods. “Gatorade is a winning brand 
with a proven formula and the science to back it up. That’s why I wanted to 
partner with them. I’m eager to launch my first signature product in a few 
months and look forward to developing additional sports performance beverages 
with Gatorade in the coming years.” The first product, Gatorade Tiger, is a 
Gatorade Thirst Quencher subline that will be launched in March 2008. The 
subline charts new territory for Gatorade, as it marks the first-ever licensing 
deal for the $5 billion brand and Tiger Woods’ first-ever endorsed sports 
beverage.<br>
<br>
“This relationship is built on The Gatorade Company’s unparalleled understanding 
of athlete needs and Tiger’s unique position as one of the world’s best 
athletes. By bringing these two leaders together, we feel we have an 
unprecedented opportunity to provide both Tiger and athletes at all levels the 
products they need to train and compete at their best,” said Todd Magazine, 
President of Gatorade.<br>
<br>
“As a first step in our relationship, Tiger recently underwent sweat analysis 
testing with the Gatorade Sports Science Institute (GSSI), which showcases his 
dedication to this collaboration and to improving any aspect of his training and 
competition.” The in-depth scientific sweat testing, conducted by scientists 
from GSSI, was designed to shape Woods’ own specialized hydration and nutrition 
strategy. A measurement of sweat rate, sweat electrolyte concentration, fluid 
and electrolyte balance and energy needs were analyzed during a vigorous round 
of exercise.<br>
<br>
“As we have done with many athletes, our testing with Tiger will help him 
prepare for, and recover from, his toughest workouts and most intense 
competitions – by meeting his hydration, recovery, nutrition and energy needs,” 
said Bob Murray, Ph.D., Director of the Gatorade Sports Science Institute. “Not 
only will Tiger personally benefit from our sports performance testing, but we 
continue to build our overall knowledge about athletes at all levels and all 
sports. Of course, this research with Tiger will also help shape the development 
of the products under his signature line.” Gatorade Tiger will be available in 
three new and refreshing flavors inspired and selected by Woods (cherry blend, 
citrus blend &amp; grape) and will be packaged in a new 500 ml bottle and a 32oz 
bottle. Gatorade Tiger will be the same scientifically proven formula of 
Gatorade Thirst Quencher.<br>
<br>
As with previous Gatorade sublines like Gatorade AM (2007) and Gatorade Rain 
(2006), the Tiger subline will provide advanced hydration (fluids and 
electrolytes) and carbohydrate energy to fuel athletic performance. The subline 
will be positioned to capture the unique qualities of Tiger that have led to 61 
wins on the PGA tour. Additional sports performance innovations from Gatorade 
and Tiger Woods will be introduced in subsequent years.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
