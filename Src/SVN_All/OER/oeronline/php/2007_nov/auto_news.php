        prototype from SEAT</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="auto5.jpg" align="left" hspace="6" vspace="4">SEAT’s new innovation, Tribu, has a robust yet sporty look with a 
        high-performance diesel engine and environmental compatibility. The 
        well-defined shapes and sharper body contours of this innovative 
        prototype increase the surface tension of the components and mark a 
        change from the current design of all SEAT models. The Tribu comes in 
        three different modes combined in one body. Depending on whether the 
        urban, sport or the free-run mode is selected, the technical parameters 
        of the new SEAT concept car, such as the engine management, suspension 
        and transmission, have been adapted. The new Tribu displays all the 
        features of an SUV as well as traditional four-wheel-drive vehicles. The 
        latest technology LED lights are used both on the rear lights and the 
        headlights, offering the best possible guarantee of seeing and being 
        seen.<br>
