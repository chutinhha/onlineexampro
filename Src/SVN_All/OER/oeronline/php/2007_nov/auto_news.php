        </font><font face="Verdana" size="2" color="#CC3300"><b>OTE launches 
        Hummer Zone</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="auto7.jpg" width="250" height="164" align="left" hspace="6" vspace="4">OTE has constructed the first-of-its-kind off-road track, ‘Hummer Zone’, 
        in the GCC behind its showroom in Wattayah and it has transplanted eight 
        heavy-duty obstacles from the wilds to the city to show what the marquee 
        is made up of. First up is the Swiss Mountain, comprising firstly of one 
        run of 16-inch vertical steps, followed by a 45-degree side-slope and 
        then a 60 per cent hill climb. The Twister is designed to showcase 
        Hummer’s body strength and the brand’s blue-chip differential lock 
        system. Next is the Flood Fight, which has the water fjord, and it 
        showcases both Hummer models’ capability to go through deep water. 
        Boulder Dash follows and this demonstrates the car’s ability to tackle 
        rocky terrain. Wood Logs shows how the cars can tackle obstacles on 
        slippery surfaces while Trench Drive brings Hummer’s axle articulation 
        and suspension flex into play on some truly off-road conditions. <br>
