        ensures easy rides. At the same time, the usual basic requirements have 
        been integrated, including minimum weight and maximum resistance to 
        lacerations. A high-grade circumferential belt has also been 
        incorporated within the structure to ensure that the tyre maintains its 
        shape even at high speeds. Pirelli tyres are marketed in Oman by their 
        authorised sole distributor General Trading Enterprises.<br>
