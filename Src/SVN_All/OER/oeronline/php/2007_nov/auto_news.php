</font><font face="Verdana" size="2" color="#CC3300"><b>Chery in Oman </b>
</font>
        <font face="Verdana" size="1"><br>
<img border="0" src="auto4.jpg" width="150" height="130" align="right" hspace="6" vspace="4">For any car enthusiast looking for impressive features, the Chery Eastar is the 
answer for them. With sophisticated parking sensors and a radar, the Eastar has 
perfect safety measures. Equipped with automatic climate control, electrically 
operated driver seat (8-way), 6-CD changer, alloy wheels, fog lamps and full 
wheel cover, amongst others, the Chery Eastar is elegant and luxurious and 
comfortable inside. Popular in Oman, Chery is now designing a large array of 
cars with advanced technologies. Take its flagship SUV, Tiggo that was recently 
launched in Oman. Popularly known as a complete fun SUV, Tiggo is sturdy but 
comfortable, is rugged but has a sporty look and brings the feel of adventure 
driving into city ride. Its major features include dual airbags, fog lamps, roof 
rack, full wheel covers, full power options, electrically operated outside 
mirrors, side impact beams (doors) and keyless entry. In Oman, the Tiggo comes 
with 2.0L and 2.4L, 4-cylinder engines in both manual and automatic 
transmissions, and is available through Chery’s showroom at Wattayah.<br>
