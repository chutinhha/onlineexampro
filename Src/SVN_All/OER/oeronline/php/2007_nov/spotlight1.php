<font face="Verdana" size="1"><br>
Every business has its own set of inherent risks and hence specific insurance 
requirements. Deciding on an adequate cover for your business is prudent as 
insurance protects not only your ‘assets’ but also your ‘liabilities’, ‘profits’ 
and ‘losses’ from the adverse impacts of business.<br>
<br>
Your physical ‘assets’ – office, factory, go-down, equipments, plant &amp; 
machinery, computers etc. can be covered under fire policy, property all risk 
policy, machinery breakdown policy, electronic equipment policy or computer all 
risk policy. Monetary assets – both in transit and within premises, find a cover 
with money insurance. Your human assets – your employees - can be given 
protection with personal accident insurance and health insurance. While 
travelling abroad, they can feel safe with a travel policy.<br>
<br>
‘Liabilities’ are an essential item of your balance sheet. It is wise to cover 
yourself and your business against all kinds of liabilities. While public 
liability cover protects the business against any legal liability arising out of 
third party injury or property damage; product liability protects you against 
legal liability arising due to any damage caused by your product to a third 
party or to their property. Professional indemnity is meant for professionals 
offering service or advice like doctors, lawyers, engineers etc. It covers you 
against any claims arising out of bodily injury or death caused by error, 
omission and negligence. Similarly, directors’ and officers’ liability cover 
provides protection against wrong financial decisions taken by the directors.<br>
<br>
‘Profits’ are universally desired, but ‘losses’ can not be totally avoided. They 
can, however, be reduced by opting for loss of profit or business interruption 
cover. Losses incurred due to fraud or dishonesty of employees in the course of 
their duties may be covered with fidelity insurance.<br>
<br>
A good businessman always strives for an all round growth of his business. 
Obtaining optimum insurance cover protects the business to the state it was 
before an unfortunate accident. However insurance cannot cover you for the 
resultant effects like loss of name, loss of market share etc. Hence it is 
always prudent to take precautions and to manage the risks to avoid losses. 
Insurance is always there should the unexpected happen!</font><p align="center" style="margin: 3px 10px">
