        <p align="justify"><b><font face="Verdana" size="4"><br>
        </font></b><b><font face="Verdana" size="4">Principal Dissident<br>
        </font></b><font face="Verdana" size="1">
        <br>
        <b>Part of what makes ‘Fooled by Randomness’ so good is Taleb’s ability 
        to make mathematical concepts entirely relevant in evaluating and 
        understanding everything from the stock market to the success of 
        millionaires</b><br>
        <br>
        <img border="0" src="brow.jpg" width="200" height="305" align="left" hspace="6" vspace="4">Sometime in early 2002, I was reading a lengthy piece by Malcolm 
        Gladwell – yes, the author of Blink and The Tipping Point – on Nassim 
        Nicholas Taleb, a Greek Orthodox Lebanese Wall Street financial genius, 
        who was described as “Wall Street’s principal dissident”. The urge to 
        lay my hands on books by that genius could not be satisfied until a few 
        weeks ago, and where would that be but – at the Turtles at the Seeb 
        International Airport departure lounge. Gladwell was reviewing Fooled by 
        Randomness: The Hidden Role of Chance in Life and in the Markets for the 
        venerable New Yorker magazine.<br>
        <br>
        Taleb’s theory is simple: Moneybags and big daddies make oodles of 
        moolah not because of their expertise and insight into the market, but 
        because Madame Luck has a role to play. We may or may not agree, but 
        Taleb does. There is much uncertainty in the world and the financial 
        markets play games, banking on these speculations and projections that 
        may turn out to be true or may not, surmises Taleb, whose father and 
        grandfather were once the Deputy Prime Ministers of Lebanon.<br>
        <br>
        By and large, big investors play safe by betting that the markets will 
        move up in small doses and discount the chances of big blips as rare. 
        Against this, Taleb believes in the ‘black swan’ theory. Taleb’s 
        favourite quote is from David Hume: “No amount of observations of white 
        swans can allow the inference that all swans are white, but the 
        observation of a single black swan is sufficient to refute that 
        conclusion. To put it simply: Taleb bets on the probability of something 
        other than what others believe in. Call him a contrarian, if you wish.<br>
        <br>
        <b>Believe in chance</b><br>
        According to him, the past (or read experience) is no guarantee of 
        future performance. Anything can happen. The Sean-Connery lookalike 
        late-40s financial wizard is out with his next book. Can you guess the 
        title? … The Black Swan: The Impact of Highly Improbable! … already a 
        best seller in the non-fiction category for the past several months and 
        which is receiving rave reviews.<br>
        <br>
        Taleb’s style of investment – he does not buy stocks, but prefers 
        options instead, because buying stocks is tantamount to investing in a 
        better future, which goes against his grain! Another rationale for 
        adopting the option route is that he does not have to shell out the 
        market price of his buys. Instead, he is ready to pay a fraction of the 
        market price and in case he loses, his loss is tiny. If, and that’s a 
        big ‘If’, he were to win, meaning his predictions turn out to be true, 
        his upside is HUGE. Though he has posted losses consistently for his 
        clients, they have not left him. Who does not want to play safe for a 
        small price? When Jean Patrice, one of Taleb’s several mentors, spoke of 
        a plane crashing into a tall building in the US much before September 
        11, 2001, he was jeered at and labelled as a nutty professor. Taleb too 
        felt the same way, until that fateful day. Nothing is absurd? Fooled by 
        Randomness is a breeze. When Taleb claims in his preface that the book 
        was “written for fun and it aims to be read principally for and with 
        pleasure,” he means it. Grab it. You may or may not agree with him. It 
        is worth every single baiza spent. So also the time spent devouring it.</font></p>
