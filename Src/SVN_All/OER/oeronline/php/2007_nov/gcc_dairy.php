        <p align="justify"><font face="Verdana" size="2" color="#CC3300"><b>
        Bahrain to go nuclear</b></font><font face="Verdana" size="1"><br>
        His Majesty King Hamad has declared that Bahrain is entering the nuclear 
        age, as per local media reports. It is working with its GCC neighbours 
        to introduce nuclear energy to benefit the economy and citizens, the 
        King stated in the National Assembly. Bahrain must keep up with global 
        change and new technology if it is to continue to grow and benefit its 
        people, he said. <br>
        <br>
        “Based on our keenness to adapt to modern sciences, we have agreed to 
        join the International Atomic Energy Agency and our membership was 
        unanimously endorsed at the 51st session. This gives Bahrain the 
        opportunity to join the international community and enter, with all 
        transparency and alongside our GCC brothers, the field of peaceful use 
        of nuclear energy. The aim of our membership is to contribute to 
        limiting the proliferation of weapons of mass destruction in the region 
        and to enable our people to benefit from the civilian use of nuclear 
        energy in the areas of health, electricity, education and agriculture,” 
        King said.</font></p>
