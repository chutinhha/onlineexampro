        Kuwait firms condemn new trading rules</font><font face="Verdana" size="1"><br>
        </font></b><font face="Verdana" size="1">
        <img border="0" src="gcc3.jpg" align="right" hspace="6" vspace="4">Listed Kuwaiti firms slammed 
        the Arab state’s stock market, calling on the government and parliament 
        to stop what they called rule changes that would encourage firms to flee 
        abroad. Almost a quarter of the firms listed on the second-largest Arab 
        exchange criticised the new rules that would halt trading in listed 
        companies that buy into or sell out to firms it had previously turned 
        down for listing. The new rule is the latest in a series of changes 
        aimed at improving transparency and shareholder rights on the stock 
        market. Exchange Director Saleh al-Falah has rejected the criticism, 
        saying the new rules were aimed at protecting investors, particularly 
        small shareholders. But stepping up pressure on the exchange, the 43 
        listed firms have accused it of going beyond its mandate by taking 
        decisions that they said broke the law, and would force firms to flee 
        Kuwait and deter foreign investment.<br>
        <br>
        “It needs the intervention of the executive and legislative powers... to 
        stop the overstepping of boundaries by the exchange in order to protect 
        Kuwait’s reputation and interests,” the companies said in page-long 
        advertisement published in the local press. The exchange had sparked 
        criticism from executives and analysts earlier this year when it said it 
        would halt trading for a year in companies that had more than quadrupled 
        their share capital. Executives have also described the listing rules as 
        too tough.<br>
