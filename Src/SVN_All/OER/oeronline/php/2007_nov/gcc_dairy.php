		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
		</font><b><font size="4" face="Verdana">Legal structures tower in 
        investment landscape</font></b><font size="1" face="Verdana"><br>
        <b>A new law implemented by Dubai is giving a greater stability to the 
        real estate market</b><br>
        <br>
        <img border="0" src="gcc.jpg" align="right" hspace="6" vspace="4">The real estate boom in the Gulf and the wider Middle East is often 
        described, even defined, by the buildings it produces: the tallest, the 
        most technologically advanced, or the development with the biggest price 
        tag.<br>
        <br>
        But new structures are appearing on the landscape and they are arguably 
        causing greater excitement in the investment community. These structures 
        are legal and could spur investment activity in the region.<br>
        <br>
        Having pioneered freehold development in the region, Dubai is once again 
        taking a lead with the passing of Law No 8, a prosaically named law that 
        is regarded by many as critical to the maturing market.<br>
        <br>
        The creation of trust accounts (escrow accounts) means that from now on 
        investors buying property “off plan” will make their payments into 
        special accounts managed by major banks and supervised by the Dubai 
        Lands Department. Developers will then have to fulfil a number of 
        conditions in terms of construction progress before they can access the 
        buyer’s money. It ends potential buyers nightmare that a rogue developer 
        might make off with their money before completing construction.<br>
        <br>
        The new law is seen as a key move to give the market greater stability 
        and many in the industry feel it will provide a major boost to investor 
        confidence, even to the point of inducing institutional investors to 
        re-look at Dubai and the region.<br>
        <br>
        “I believe that the market will see a new wave of investors in the real 
        estate sector, with a completely different demographic profile from 
        buyers who have been investing in the Emirate for the last five years, 
        which was when Dubai opened up the property market to foreigners,” said 
        Mehdi Amjad, President and Chief Executive of Omniyat Holdings and 
        Omniyat Properties.<br>
        <br>
        <img border="0" src="gcc1.jpg" width="250" height="281" align="left" hspace="6" vspace="4">He said that the new law would lead to more transparency and confidence 
        in the market. “We are very happy with this new law because it’s 
        positive for serious developer.”<br>
        <br>
        Amjad said that institutional investors had so far been reluctant to 
        invest in off-plan developments because of the lack of measures to 
        protect their investments, something the new law aims at achieving. 
        “This is a natural progression for the Dubai market and will ensure its 
        continuity.”<br>
        <br>
        Amjad also praised the way that the law was structured after an 
        extensive consultation process between the government, major developers 
        and banks that will take part in the scheme. In fact, Omniyat alone was 
        in discussion with the Dubai Lands Department for six months.<br>
        <br>
        “The law was structured in a very clever and efficient way, so that 
        rather than having control by the government, causing more pressure on 
        the land department, they have networked reputable banks to work with 
        the developers,” he said. “This is very efficient and another innovation 
        by the government of Dubai, which wants to reduce bureaucracy and red 
        tape.”<br>
        <br>
        Under the new scheme, developers simply need to go to the Dubai Lands 
        Department and register a new property. The department will then issue 
        instructions to the four approved banks to open a trust account in the 
        name of the project. Prospective buyers will make their payments into 
        such accounts rather than to developer.<br>
        <br>
        The new law was enacted even as the Dubai Lands Department was being re-organised 
        to enable it to better cope with the large number of freehold property 
        registrations together with changes in the Emirate’s regulatory 
        framework. A new unit, the Real Estate Regulatory Agency, was 
        established to oversee regulations, including trust accounts, new laws 
        covering real estate brokers and planned new regulations on home owners’ 
        associations.<br>
        <br>
        Dubai is not alone in re-organising legislations relating to the real 
        estate sector. In Abu Dhabi too, the government is reviewing its laws, 
        with a view to clarifying key issues relating to home ownership among 
        UAE nationals and foreigners.<br>
        <br>
        Developers have also been contributing to a more investor-friendly 
        environment. Emaar, for instance, has almost eliminated a controversial 
        charge paid by homeowners’ when they want to sell their property. 
        Accordingly, it has reduced the charge for a Notification of Completion 
        certificate by 90 per cent, from Dhs5,000 to Dhs500. This followed an 
        earlier move by competitor Nakheel to halve the cost of such 
        certificates, from Dhs1,000.<br>
        <br>
        The regulatory moves are welcome in a region where the figures for real 
        estate development continue to impress and are projected to continue 
        doing so until the end of the decade, according to the Middle East 
        Economic Digest. Says the Digest:</font></p>
        <ul>
          <li>
          <p align="justify"><font size="1" face="Verdana">Saudi Arabia’s programme of building special economic cities is expected 
        to attract more than US$80 billion in domestic and foreign investment. 
        The Saudi Arabian General Investment Authority (SAGIA), the body 
        responsible for with attracting inward investment, says that cities are 
        a key part of the country’s strategy to become one of the world’s top 10 
        most competitive nations.</font></p></li>
          <li>
          <p align="justify"><font size="1" face="Verdana">Abu Dhabi has current and announced real estate projects valued at 
        US$272 billion.</font></p></li>
          <li>
          <p align="justify"><font size="1" face="Verdana">Although on a much more modest scale, some of the UAE’s northern 
        Emirates are attracting a lot of investor interest. Ras Al Khaimah is 
        particularly active, with much interest in developments such as La Hoya 
        Bay and The Cove.</font></p></li>
          <li>
          <p align="justify"><font size="1" face="Verdana">The regional retail real estate market is the fastest growing in the 
        world, with more than 16.3 million sq. metres of gross leasable area 
        scheduled for completion by 2010, according to Colliers International.</font></p>
          </li>
        </ul>
        <p align="justify"><font size="1" face="Verdana">Having turned the desert into highly bankable real estate, the 
        Gulf-based industry is currently investing many billions further afield, 
        from India and Pakistan to Egypt and North Africa , taking and adapting 
        real estate models that have worked well in the Gulf to these markets.<br>
        <br>In terms of pure investment, Arab investors could spend as much as US$20 
        billion on real estate worldwide in 2007, according to Jones Lang 
        LaSalle. This represents a 60 per cent increase on the US$13 billion 
        they spent in 2006. The prime destinations for that money were the US, 
        UK, South Africa and Germany.<br><br>Similarly, Hedge Fund Research estimates that the amount of Middle 
        Eastern capital available for investment is a staggering US$4.1 
        trillion, making the region one of the most valuable in terms of 
        investment capital.<br><br>On a wider front, global asset management group Merrill Lynch has noted 
        a renewed trend towards investment in real estate worldwide. In its 
        World Wealth Report, it says the trend was away from assets such as 
        hedge funds, derivatives, commodities and private equity.<br><br>The report, covering the assets of the world’s wealthy elite, identified 
        the UAE as having 68,100 millionaires, up 9,100 on the previous year. In 
        neighbouring Saudi Arabia, the number was 89,000. Worldwide, the number 
        of high net worth individuals was put at 9.5 million, up 8.3 per cent on 
        year. Ultra high net worth individuals (those with US$30 million or more 
        in liquid assets) rose by 11.3 per cent to 94,970. The combined assets 
        of the “millionaire class” were put at US$37.2 trillion.<br><br>The man once called “the best real estate investor in the world” has 
        given his stamp of approval on the regional market. Expressing a degree 
        of disillusionment with the market in the US, billionaire Thomas J. 
        Barrack says: “To find true growth areas, you have to look East, from 
        Dubai to Shanghai – the future is there.”<br><br>The UAE’s leadership role in the region’s real estate market has also 
        been acknowledged by a Johns Hopkins University study, which says: “One 
        initial thought can be presented: the UAE is the first country in the 
        world to perform major urban transformations in the 21st century and it 
        poses a great opportunity for investors willing to generate a new 
        pattern of real estate investments in the world.”</font></p>
