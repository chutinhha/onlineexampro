        studies the future of Saudi Arabia</b></font><font face="Verdana" size="1"><br>
        The World Economic Forum has released its latest scenario study – The 
        Kingdom of Saudi Arabia and the World: Scenarios to 2025, a set of 
        scenarios looking at three possible and plausible futures for Saudi 
        Arabia. These scenarios examine potential shifts across economic, 
        environmental, social, political and technological spheres and offer 
        challenging visions designed to test strategy, open up dialogue, and 
        offer insight into important factors affecting Saudi Arabia and the 
        broader region between today and 2025.<br>
        <br>
        The scenarios analyse medium- and long-term risks and development 
        opportunities for Saudi Arabia and emphasise the importance of 
        transparency, regional relationships and shifting social factors among 
        the forces that will impact the Kingdom of Saudi Arabia in the year 
        2025.<br>
        <br>
        To further explore the deeper issues and ramifications of the region’s 
        development, the World Economic Forum will take the results of The 
        Kingdom of Saudi Arabia &amp; the World: Scenarios to 2025 to the World 
        Economic Forum Annual Meeting 2008 in January, to be held in Davos.
