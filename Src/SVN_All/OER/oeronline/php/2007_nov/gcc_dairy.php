        <p align="justify"><font face="Verdana" size="2" color="#CC3300"><b>
        Saudi Arabia is top FDI recipient in Arab world</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="gcc11.jpg" align="right" hspace="6" vspace="4">Saudi Arabia was the largest recipient of foreign direct investment (FDI) 
        in the Arab world in 2006, attracting US$18 billion, an increase of 51 
        per cent over 2005, according to the latest report of the United Nations 
        Conference on Trade and Development (UNCTAD).<br>
        <br>
        The report attributed the increase to the region’s strong economic 
        growth and improved business climate and to high oil prices, which have 
        attracted increasing amounts of FDI to oil, gas and related industries 
        as well as to telecom, tourism and financial services.<br>
        <br>
        The unprecedented increase in FDI came after Saudi Arabia took a series 
        of measures to attract foreign investment. The country’s Supreme 
        Economic Council (SEC) revised the negative list last March, allowing 
        foreign investment in vital sectors like insurance services, wholesale 
        and retail trade, air and train transport, and communication services.<br>
        <br>
        According to the UNCTAD report, FDI inflows to the 14 economies of West 
        Asia surged by 44 per cent in 2006 to an unprecedented US$60 billion. 
        Turkey topped the list with US$20 billion, a 100 per cent increase over 
        2005, Saudi Arabia second with US$18 billion and the UAE third with US$8 
        billion, with a 23 per cent decline in inflows or US$8 billion. Jordan 
        with US$3.1 billion FDIs and Bahrain with US$2.9 billion, were on fourth 
        and fifth positions, respectively.<br>
        <br>
        FDI outflows from West Asia totalled US$14 billion, an increase of 5 per 
        cent over the 2005 level. The value of cross-border mergers and 
        acquisitions (M&amp;As) by investors from West Asia increased by 78 per 
        cent, amounting to US$32 billion, as a result of high oil prices and the 
        current account surpluses of oil-producing countries. About two-thirds 
        of those M&amp;As were targeted at developed countries, in particular the 
        United Kingdom (35 per cent by value), Canada (11 per cent) and the 
        United States (9 per cent). Kuwait accounted for more than half of the 
        region’s total outward FDI, mainly in the telecommunications industry.</font></p>
        <p align="justify"><font face="Verdana" size="2" color="#CC3300"><b>Huge 
        numbers of camels dying in Saudi Arabia</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="gcc12.jpg" align="left" hspace="6" vspace="4">More than 2,000 dromedaries – Arabian camels – have died since August 10 
        in Saudi Arabia, according to Science Daily. Various theories have been 
        put forward to explain the numerous deaths. Analyses are being conducted 
        to confirm or rule out the hypothesis of food intoxication. Poisoning 
        caused by the ingestion of toxic plants, mycotoxins (microscopic fungi), 
        or mineral deficiencies have also been suggested. The deaths are 
        probably due to a multitude of factors, which have a detrimental effect 
        on the immune system, including some viruses which could increase the 
        severity of infections or parasitic infestations in animals.</font></p>
