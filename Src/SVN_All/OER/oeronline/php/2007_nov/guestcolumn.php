<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">
Towards a free trade regime</font></b><font face="Verdana" size="1"><br>
<b>The Abuja Treaty agreed to in May 1994 has the same significance to Africa as 
the Treaty of Rome has for European integration. SADC has the same significance 
for the Southern African region as AGCC has for the Gulf. HE Yacoob Abba Omar 
contributes to this issue by addressing the challenges and prospects for 
regional integration in his part of the world<br>
</b><br>
<img border="0" src="guest.jpg" width="154" height="259" align="right" hspace="6" vspace="4">The 
IMF’s October 2007 report for the Sub-Saharan African region says that the 
region has “experienced its strongest growth and lowest inflation in over 30 
years. Growth in sub-Saharan Africa should reach 6 percent in 2007 and 6.75 
percent in 2008”. These are remarkable figures for a region which has an image 
of poverty and disasters. <br>
<br>
The establishment of Africa-wide bodies which have been pushing democratic 
reforms, better governance and improved economic performance can justifiably lay 
claim to these developments. The year 1994 has a special significance in the 
African calendar. That was the year in which South Africans had their first 
democratic elections, ensuring the liberation of the one African country which 
remained under some form of colonial rule.<br>
&nbsp;<br>
It was also the year in which leaders of the African continent gathered in 
Abuja, Nigeria to commit themselves to the creation of an African Economic 
Community (AEC). The Abuja Treaty detailed the vision for the establishment of 
AEC by 2028. It set out six stages which need to be passed before we could 
achieve that. At the core of the process is the strengthening of the regional 
economic communities (RECs), of which the Southern African Development Community 
(SADC) is one. The SADC consists of South Africa and Tanzania, two countries 
which have diplomatic representation in Oman, as well as Angola, Botswana, 
Democratic Republic of Congo (DRC), Lesotho, Malawi, Mauritius, Madagascar, 
Mozambique, Namibia, the Seychelles, Swaziland, Zambia and Zimbabwe. <br>
<br>
According to the Abuja Treaty, the path to the AEC lie in:</font><ul>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Strengthening the RECs, and where they did not exist, to help create them. The 
  following RECs apart from SADC are now in place: Economic Community of West 
  African States (Ecowas), Community Market for Eastern and Southern African 
  States (Comesa), one for the central African countries and one for the North 
  African states.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Stabilising tariff and other barriers as well as achieving sectoral 
  integration.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Establishing FTAs/Customs Unions for each REC. This would be followed by 
  harmonization of tariffs amongst RECs.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Establishing an Africa-wide Customs Union. This must contribute to having a 
  single set of external tariffs and revenues from that being shared in an 
  equitable manner.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Electing a Pan-African Parliament (PAP) - currently the delegates to PAP are 
  nominated by the member countries. This can then lead to the African Economic 
  and Monetary Union, a Central Bank and a single currency. There have already 
  been suggestions that the currency be called the ‘Swahili’ – a language which 
  is being promoted as Africa’s lingua franca.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
  Implementing the African Monetary programme by 2021 which can lead to the AEC.</font></li>
</ul>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
<img border="0" src="guest1.jpg" align="left" hspace="6" vspace="4">The 
IMF’s October 2007 report illustrates the strides already made. It states that 
“continued progress in stabilizing economies and implementing reforms in most 
countries has helped sustain rising investment and productivity. <br>
<br>
“In the current expansion, many countries have benefited from terms of trade, 
but there is also signs that macroeconomic and policy conditions continue to 
improve, which should help sustain growth”. The report also identifies the 
decline in armed conflict and political instability as major contributors to 
growth.<br>
<br>
Given the important role trade plays in improving growth, the SADC region has 
decided to fast track the move towards a regional FTA. It aims to establish that 
by 2008, when 85 percent of goods traded in the region should have zero tariffs. 
The aim is to have this raised to 100 percent by 2012. It wants to have a SADC 
Customs Union in place by 2010 so that it can have a common external tariff. The 
next milestones would be a SADC Common Market by 2015, a SADC Monetary Union by 
2016, and a single currency by 2018. <br>
<br>
South Africa has had some measure of experience in monetary harmonization 
through the Southern African Customs Union (SACU), which includes Botswana, 
Namibia, Lesotho and Swaziland. SACU shares a common tariff regime without any 
internal barriers. Customs revenues are shared according to an agreed formula. 
As Gabriel Nahimana, writing for Project Syndicate, wrote about Southern Africa: 
“Protocols and political treaties are not sufficient to boost integration. The 
major barrier is the region’s great diversity in economic and institutional 
development. The SADC’s regional plan establishes a time frame for policy 
implementation over the next fifteen years that takes these constraints into 
account.”<br>
<br>
Speaking at the launch of the Communications Strategy and Plan of Action for the 
SADC FTA on 10 October 2007 at SADC Head Office in Gaborone, Botswana, the 
Executive Secretary of SADC, Dr Tomaz Augusto Salomão expressed confidence on 
SADC achieving that objective. He said: “An audit study was carried by the 
Secretariat to examine the gazetted tariff phase programme. I am happy to 
announce to you that the results of the audit study are positive and indeed 
validate a concrete basis for the launch of the FTA in 2008. Specific actions to 
support those member states that are lacking in implementing fully the tariff 
phase-down programme have been put in place. I am confident, therefore, that all 
outstanding issues would be resolved before the launch of the FTA in 2008.”<br>
<br>
President Mbeki expressed our concerns succinctly when he said that care must be 
taken that “the obviously dominant position of the South African economy does 
not serve further to entrench the underdevelopment of the other SADC members.”<br>
<br>
Since 2000, when South Africa began implementing a free trade system for the 
region to 2005, our exports to the region have increased from $30 billion to $45 
billion. Imports from the region increased from $3.5 billion to $10 billion. 
These figures starkly highlight the challenge the region faces in ensuring a 
freer trade regime. If SADC gets this right it could benefit hugely. One report 
has shown that eliminating all restrictions on international travel in the 
region could mean an extra half a million tourists every year. This would 
translate into the region’s GDP increasing by about $1.5 billion, helping create 
70 000 new jobs.<br>
<br>
Now that is something to look forward to! </font></p>
