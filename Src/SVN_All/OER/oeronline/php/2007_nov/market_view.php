<b>The recent rise in the MSM index indicates a return of FIIs. OER reports<br>
</b><br>
Hectic buying activity pushed up stock prices in the Muscat Securities Market 
during the one-month period ended October 15, 2007. The benchmark MSM-30 general 
index, which has so far generated 32.9 per cent year to date return, zoomed by 
12.3 per cent or 818 points during the period. This was one of the biggest 
month-on-month gains in the history of MSM and was primarily due to heavy buying 
in the Omantel counter. The latter also led to the rise in services sector index 
by 13.5 per cent. This was followed by a 7.2 per cent rise in the banking and 
investment Index and 6.6 per cent rise in the industry index. Market turnover 
too saw substantial increase at RO168 million.<br>
<br>
On absolute returns basis, MSM stands ahead of all other GCC and MENA bourses, 
which is an indication that despite low market depth, institutions from foreign 
locations have started accumulating shares in Oman. Refunds from Galfar IPO 
could also have got channeled into local stocks, generating further demand. In 
key corporate events, the shareholders of BankMuscat (SAOG) approved the 
proposal by the Board of Directors to raise capital through private placement of 
15 per cent of its shares with Dubai Financial Group, a Dubai Group company, for 
RO238.3 million (USD619 million). In another development, Omantel announced that 
the company’s board had approved acquiring a majority stake in Worldcall Ltd, 
the Pakistan-based telecom company. Finalisation of the deal is subject to the 
approval of Worldcall shareholders and the Pakistani regulatory authorities. 
However, the counter saw heavy buying on news that the committee set up to 
revamp Omantel had agreed to reduce the royalty being paid by Omantel and its 
competitor Nawras for fixed and mobile phone services to 7 per cent each, 
instead of 10 per cent and 12 per cent, respectively. Among the other measures 
taken by the government was its approval to sell a portion of its stake in 
Omantel to a strategic investor.<br>
<br>
Renaissance Services announced that its Abu Dhabi based oil and gas subsidiary 
Adyard LLC has been awarded a US$35-million contract by Single Buoy Moorings 
Inc. for the fabrication of a mobile offshore production unit for Talisman 
Energy to be located in the Norwegian sector of the North Sea. In company 
results announced for the third quarter, Oman Cables announced that net profit 
for nine months ended September 30, 2007, was RO10.056 million compared to 
RO7.013 million last year, an increase of 43 per cent. The counter witnessed 
heavy buying with the results announcement.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
