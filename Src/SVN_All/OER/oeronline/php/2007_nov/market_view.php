<font size="1"><font face="Verdana"><b>GCC MARKET COMMENTARY</b><br>
For the month ended October 15, 2007, all GCC markets ended on a positive note, 
with the Abu Dhabi bourse gaining the most at 11.49 per cent. This was on the 
back of huge buying seen in the real estate counters, with the real estate index 
gaining 25.75 per cent during the period. The industrial and banking counters 
also performed well by rising 11.47 per cent and 10.44 per cent, respectively. 
The Dubai exchange was the next best performer gaining 10.81 per cent during the 
period. This was due to overall interest in the market with the real estate and 
investment indices in particular gaining 13.92 per cent and 12.96 per cent, 
respectively. The Doha exchange too closed with a double-digit growth gaining 
10.13 per cent. The industrial index was the top gainer and increased by 18.43 
per cent during the period. The Kuwaiti and Bahraini bourses grew by 2.30 per 
cent and 2.00 per cent, respectively. While the banking sector was the top 
gainer on the Kuwaiti exchange gaining 6.56 per cent, the investment index 
topped the list on the Bahraini bourse gaining 5.04 per cent. The Saudi exchange 
was the lowest return generator during the period gaining 1.22 per cent. Most of 
the broader indices here ended in the red; however, the industrial index was 
positive with a gain of 5.54 per cent during the period. <br>
