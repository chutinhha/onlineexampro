</font><b><font face="Verdana" size="2" color="#CC3300">India’s NTPC seeks partners for three power projects</font></b><font face="Verdana" size="1"><br>
India’s biggest power producer National Thermal Power Company (NTPC) is seeking 
joint venture partners from Oman for developing three large-sized power projects 
in the southern part of the country. “We have identified three power projects 
for foreign equity participation,” T Sankaralingam, Chairman and Managing 
Director of NTPC said.<br>
<br>
These are 1,950-MW gas-based power project in the south Indian state of Kerala 
and another two coal-based power projects (each having 1000-MW capacity) 
proposed in Tamil Nadu – one at Ennore near Chennai and another at Cheyyur in 
Kanchipuram. Top-level NTPC officials will have one-to-one meeting with 
businessmen groups from Oman. The first power project proposed at Kayamkulam, 
near NTPC’s existing power plant in Kerala, will cost US$1.48 billion. Of the 
other two power stations, Ennore plant will have an envisaged investment of 
US$1.13 billion, while the other one will cost US$1.16 billion.<br>
<br>
“We are thinking of offering 11-25 per cent stake in each of the three 
projects,” Sankaralingam noted. All these projects are envisaged with a 70:30 
debt equity ratio. The proposed power plants in Tamil Nadu are joint ventures 
with the state’s electricity board and one or more foreign investors will be 
considered as joint promoters for all the three projects. The Kerala project 
will be implemented in two phases – the first phase of 650 MW capacity will go 
on stream by 2010-2011 and the second phase of 1,300 MW will be ready by 
2011-2012, said A K Sharma, General Manager of NTPC.<br>
