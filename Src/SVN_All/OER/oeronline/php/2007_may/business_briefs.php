</font><b><font face="Verdana" size="2" color="#CC3300">Kempinski Hotels
ties up with The Wave</font></b><font face="Verdana" size="1"><br>
<img border="0" src="bus.jpg" align="right" hspace="6" vspace="4">Oman’s effort to become a preferred destination for business and leisure 
travellers has got a big boost as Kempinski Hotels is set to launch a 
world-class 300-room luxury hotel at The Wave project. The senior management of 
The Wave and Kempinski Hotels signed the agreement on 26th April at Abu Dhabi’s 
Emirates Palace, one of the finest hotel properties across the globe, managed by 
Kempinski Hotels. <br>
<br>
To be built at an estimated investment of US$100-120 million, Kempinski Hotels 
at The Wave is expected to be ready by 2010. Once operational, it is going to 
generate 500-600 new jobs. His Excellency Abdulla Bin Abbas Bin Ahmed, Chairman 
of Muscat Municipality and Chairman of the Board of Directors, The Wave said, 
“With the expertise on offer from our strategic partners like Kempinski Hotels, 
the development joins the league of the top community projects in the Middle 
East. Our tie-up with Kempinski is part of our vision to involve the finest 
world-class companies in the field.”<br>
<br>
Kempinski Hotels is Europe’s oldest luxury hotel collection comprising of over 
55 luxurious properties in Europe, the Middle East and Africa, Asia and South 
America. Talking about the debut in the Sultanate, Ulrich Eckhardt, Senior Vice 
President, Middle East &amp; Africa, at Kempinski Hotels, said: “Our foray into Oman 
and presence at The Wave further reinforces our expansion plans in the Middle 
East. Our property at The Wave, Muscat will offer an exceptional range of 
business and leisure facilities, and luxury amenities within a breathtaking 
beachside location.”<br>
<br>
He added, “Oman’s rich traditional and cultural heritage will be kept in mind 
while designing the property and the product offering.” Europe especially 
Germany and UK contributes maximum international tourists to Oman. Kempinski’s, 
well known brand in Europe, presence in Oman will attract more European tourists 
to the Sultanate as they will have the comfort of staying in a familiar 
hospitality brand that also offers the essence of Omani culture.<br>
