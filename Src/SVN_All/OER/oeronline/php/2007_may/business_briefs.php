</font><b><font face="Verdana" size="2" color="#CC3300">Win an apartment with NBO’s credit card summer promotion</font></b><font face="Verdana" size="1"><br>
National Bank of Oman (NBO) has announced the launch of an exclusive credit card 
summer promotion offer for its customers. The bank is offering its customers an 
opportunity to win an apartment in Shatti Al Qurum. This is the first time ever 
a bank in Oman is offering such a high-value prize on a credit card spend. NBO’s 
credit card summer promotion will be valid from May 1 to July 31, 2007. All that 
the customers need to do is spend RO150 during the campaign period with an NBO 
credit card and avail of a chance to win an apartment in Shatti Al Qurum. 
Special arrangements have been made for expatriates to apply for the grand 
prize. In addition to winning the apartment, NBO cardholders will have a chance 
to win digital cameras and diamond/gold jewellery by spending a minimum of RO50 
per month on their credit card. During this campaign, new gold card customers 
will be given 10 extra chances at the mega prize and for new silver card 
customers there will be five extra chances. Plus new customers can also benefit 
from a free annual subscription to Times of Oman or Al Shabiba. The more you 
spend on your card, the better are your chances to win the NBO apartment as with 
every RO10 spent a customer gets an extra chance. The campaign will run from May 
1 to July 31, and at the end of every month there will be a draw for the digital 
cameras/jewellery. The apartment draw will be held at the end of the campaign. 
On the launch of this new campaign, Zahir Khurshid, NBO’s head, assets and cards 
said: “We are indeed delighted to have taken the lead once again to launch this 
unique credit card campaign, where the winner can get an apartment in Shatti Al 
Qurum and fulfil a dream of owning a home.”<br>
