<font face="Verdana" size="1">The world of commerce and industry believes strongly in time-tested traditions. 
OER Top 20 Ranking is one such tradition that has established, over the years, 
firm roots in Oman’s business community. Commemorating this annual event, we 
present the Top 20 Companies of Oman – with eminent industry expert <b>Mukhtar 
Hasan</b> analysing the ranking and <b>KPMG</b> verifying the data. <b>The Top 20 Corporate 
Profiles</b> are compiled by OER Principal Correspondent <b>P. Aneel Kumar</b>, with 
<b>Gulf 
Investment Services</b> and <b>Vision Securities</b> giving the stocks analyses.<br>
