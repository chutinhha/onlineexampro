</font><b><font face="Verdana" size="4">The Wave, Muscat<br>
</font><font face="Verdana" size="1">An Idyllic Island Lifestyle</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">Muscat, a charming cosmopolitan city founded 
nearly 900 years ago, is a perfect blend of the past and the present, as it 
offers every modern amenity while still preserving a more traditional Arabian 
spirit. To live up to the city’s international brand image, The Wave – a premium 
residential community that seeks to define coastal resort style living, 
combining Omani architectural heritage and contemporary design – is planning the 
first major integrated resort and residential development. To be located on the 
beachfront in the greater Muscat region, it is a fully master planned community 
that will occupy a total area of 2,500,000 sq.mt. along 6 km of natural beach. 
The development will soon be launching properties on Sinesla Island, the first 
of three islands on the project. In fact, it is the first residential island to 
be launched in the country.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="1" src="wave.jpg" width="400" height="248"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
Life at Sinesla Island, as the company puts it, promises not only tranquillity 
and freedom, but also exclusivity as the waterfront and garden villas on the 
island will form part of a private gated community. All the 49 waterfront villas 
on the island come with their private beaches, offering the ultimate luxury. The 
30 garden villas will offer a different but equally stirring experience with 
their delicate landscaping, spacious courtyards and contemporary architecture 
with a distinct Middle Eastern flavour. Designed to reflect the aspiration and 
purpose of the developers, the homes will ensure stylish and practical living.
<br>
<br>
The Wave’s best spots to shop, eat, or golf are just a few minutes from the 
villas’ doorstep. Residents will be in close proximity to the Marina Village, 
where they can stroll down to the restaurants, shops, cafés, to socialise and 
relax, or walk over to the beach or the golf course. It is an idyllic 
proposition, playing a hole with the ocean in the background, waves breaking on 
the shore and the sun setting in the distance. Besides, a clubhouse is being 
built right on the waters’ edge. <br>
<br>
The $1-billion multipurpose project will have a championship 18-hole green links 
golf course as well as a harbour front village facing a 300-berth marina. 
World-renowned golfing maestro Greg Norman has been selected as architect for 
The Wave, Muscat’s world-class links golf course. The signature golf course, the 
first sea-facing course in the Sultanate of Oman, will be spread over 800,000 sq 
m and will be one of the best equipped facilities in the entire region, a visual 
and environmental masterpiece. The Wave, Muscat is being developed through a 
strategic partnership between the Omani government and UAE-based Majid Al 
Futtaim.<br>
<br>
The Wave believes its success will be, and has always been, focussed on good 
planning, strong organisation and the ability to visualise perfection. With all 
the key components already in place, The Wave, Muscat is all geared up to 
deliver something special. </font>
