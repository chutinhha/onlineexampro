        <p align="justify"><font face="Verdana" size="1"><b>Barwa in breakthrough solution</b><br>
        <img border="0" src="gcc6.jpg" width="150" height="74" align="left" hspace="6" vspace="4">Barwa Real Estate has announced a breakthrough solution to the housing 
        cost challenge facing Qatar’s resident workers. <br>
        <br>
        The Doha-based real estate giant unveiled plans to develop and release 
        over 2,000 modern housing units on low rentals to limited-income earners 
        in a move aimed at easing the housing shortage in the city. According to 
        Barwa executives, this is the beginning of a series of steps focussed on 
        combating the inflated real estate market. <br>
        <br>
        <b>
        <img border="0" src="gcc4.jpg" width="175" height="141" align="right" hspace="6" vspace="4"></b>The rising housing cost in Doha has been a cause for concern for 
        residents and employers alike. Though Qatar’s Ministry of Economy and 
        Commerce imposed a 10 per cent limit on annual rent increases in 2006 in 
        an effort to curb the astronomical rise in rents, the move had little 
        effect. In fact, rentals appear likely to increase further in the 
        short-term before supply catches up with demand as more construction 
        projects take shape. The brisk pace of economic growth has exposed some 
        capacity constraints in the building industry, leading to higher housing 
        costs. This has in turn pushed consumer price inflation up to around 9 
        per cent. Inflation is expected to ease slightly to around 7.5 per cent 
        in 2007 as more housing comes on stream. <br>
        <br>
        Barwa will place its 2,000 units on the market for a monthly rent of 
        QR3,500. The project will be located in two central points in Qatar’s Al 
        Sayliah and Messameir areas and the company is looking to complete 
        construction by April 2008. <br>
        <br>
        Barwa is one of the regions largest publicly listed real estate 
        developers, reporting revenue of US$889 million in 2006. Barwa is also 
        among the regions top 20 developers with US$3.76 billion invested in 
        mega projects currently under development.</font></p>
