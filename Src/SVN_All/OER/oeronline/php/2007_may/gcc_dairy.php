        <b>Phase I of ‘The Avenues’ launched</b><br>
        The first phase of Kuwait’s largest commercial development ‘The Avenues’ 
        was officially launched by the Amir of Kuwait, His Highness Sheikh Sabah 
        Al Ahmad Al Jaber Al Sabah. With an overall investment of US$1 billion 
        for the four phases which will be opened in stages, ‘The Avenues’ is set 
        to become Kuwait’s premier shopping, leisure and business destination 
        when completed in 2010.<br>
        <br>
        The first major project conceived and developed by Mabanee, Kuwait’s 
        premier real estate developer, it will host the widest selection of 
        international brands in the region spread over 216 stores, including 
        Kuwait’s first Carrefour hypermarket, the region’s largest IKEA store, 
        Cinescape, (a 10-screen Cinema complex), 39 restaurants and cafes within 
        a lively ‘Theatre of Food’. <br>
        <br>
        The mall has been designed by the world-renowned architects, Norr 
        Consultants International, a firm that has built some of the most 
        distinctive buildings in the Middle East, including the Emirates Towers 
        in Dubai. Norr was selected for the project following a rigorous 
        international design competition involving some of the world’s leading 
        architects. Al Ghanim International are the main contractors.<br>
        <br>
        Phase One comprises 170,000 sq. m of leasable area and parking for over 
        4,000 cars.<br>
&nbsp;<br>
        Phase Two, scheduled to open in early 2008, will add nearly 60,000 sq. m 
        of leisure and retail space and parking for over 2,000 cars. In its 
        third phase, set to open in late 2008, ‘The Avenues’ will add a boutique 
        mall, spa, theme restaurants, an entertainment complex, water features 
        and facilities that are new to Kuwait.After completion of the final 
        phase by 2010, the destination will feature a state-of-the-art business 
        centre, complete with four-star hotels, office space, a convention 
        centre and a theatre. The mall’s current 4,000 car parking spaces will 
        increase to 15,000. The project is being developed in a prime site 
        measuring 425,000 sq.m. <br>
        <br>
        <b>Finding private sector jobs </b> <br>
        Latest published figures point out to declining representation of 
        Bahraini nationals in jobs offered in the private sector. The issue is 
        critical partly due to absence of growth of job opportunities in the 
        public sector.<br>
        <br>
        According to General Organisation for Social Insurance (GOSI), locals 
        accounted for 21 per cent employment in private establishments in 2006, 
        thereby continuing a disturbing trend. Nationals represented 24 per cent 
        of private sector jobs in 2005 and 27 per cent in 2004. The issue is 
        vital in the light of greater involvement of private sector firms in the 
        economy, thanks to on-going reforms that call for limiting public sector 
        role. The reforms seek to bring about efficiency in the economy. It is 
        feared that the trend could deepen the job challenge for locals, as 
        private sector firms seek to generate maximum profit by operating at the 
        lowest possible cost. <br>
        <br>
        To be sure, the private sector succeeded in bringing about 14,000 new 
        jobs in 2006. The employment figures increased from 299,080 in 2005 to 
        313,039 in 2006. However, Bahrainis only accounted for 65,614 jobs in 
        2006, down from 71,900 in 2005. Thus, foreign nationals soaked up all 
        new job opportunities last year. <br>
        <br>
        Private sector firms prefer employing expatriates over locals for 
        numerous reasons, notably difference in pay. According to GOSI 
        statistics, the average salary in the private sector stood at BD210 
        (US$556) in 2006. However, the average monthly pay amounted to BD414 for 
        Bahraini nationals versus BD156 for foreign nationals. Hence, 
        expatriates received an average of 38 per cent of what locals earned.
        <br>
        <br>
        It is this difference in pay that accounts for private sector preference 
        for foreign employees. Still, employers consider other factors such as 
        productivity and customer service when evaluating employment decisions. 
        However, there are other issues, notably lack of laws that somehow 
        provide private firms the opportunity to exploit expatriates. It is 
        believed that some firms tend to delay payment of salaries when it comes 
        to foreign nationals. Still, others demand that expatriate work longer 
        than normal hours. Certainly, firms cannot apply such unacceptable 
        conditions on nationals.<br>
        <br>
        In short, employment statistics suggest that authorities must exert 
        greater efforts in order to convince private sector firms to provide job 
        opportunities to locals.</font></p><hr width="90%" size="1">
