		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        UAE leads the Arab World</font><font face="Verdana" size="1"><b><br>
        Oman ranked second best among ‘middle stage of development’ countries<br>
        <br>
        </b>The UAE is the most competitive economy in the Arab world among the 
        countries in the most advanced stage of development, according to The 
        Arab World Competitiveness Report 2007 of the World Economic Forum. The 
        UAE is followed by Qatar and Kuwait. Among countries at the second stage 
        of development, Tunisia and Oman are the best performing Arab economies 
        while Egypt is the regional best performer in the third group of 
        countries.<br>
        <br>
        This year’s report covered 13 Arab economies – Algeria, Bahrain, Egypt, 
        Jordan, Kuwait, Libya, Mauritania, Morocco, Oman, Qatar, Syria, Tunisia 
        and the UAE. Libya, Oman and Syria were assessed for the first time. The 
        report ranked the countries in three groups according to the stage of 
        development to enable benchmarking against peers in other parts of the 
        world.</font></p>
        <p align="center">
        <img border="0" src="gcc1.jpg" width="500" height="442"></p>
        <p align="justify"><font face="Verdana" size="1">Accordingly, the UAE, the best performing Arab country, has been ranked 
        the 29th among the 40 most advanced economies. Sound economic management 
        has contributed to stabilising the macroeconomic environment and 
        strengthening public institutions. Areas of concern include educational 
        attainment, in particular at the primary and secondary levels, said the 
        report, adding that these need to be tackled on a priority basis, as the 
        lack of an educated workforce could put current diversification efforts 
        at risk. The report calls for increasing efforts to promote innovation 
        and tertiary education, and steps to combat red tape that constrains 
        competition for goods and services.<br>
        <br>
        According to the report, Oman is the second best ranked Arab country in 
        the group of countries at the middle stage of development, coming in at 
        8th rank in the group of 40 peer countries. The country’s solid outcomes 
        on macroeconomic indicators, its well-developed institutions and high 
        level of efficiency of labour markets contributed to this result, said 
        the report. However, the country’s competitive position can be improved 
        upon by increasing the level of education and upgrading technological 
        capability to embrace innovation.<br>
        <br>
        The rankings are drawn from a combination of publicly available data and 
        the results of the Executive Opinion Survey, a comprehensive annual 
        survey conducted by the World Economic Forum, together with its network 
        of partner institutes (leading research institutes and business 
        organisations) in the countries covered by the report. Over 11,000 
        business leaders were polled in a record 128 economies worldwide. The 
        survey questionnaire was designed to capture a broad range of factors 
        affecting an economy’s business climate that are critical determinants 
        of sustained growth. The Forum annually delivers a comprehensive 
        overview of the main strengths and weaknesses in a large number of 
        countries, making it possible to identify key areas for policy 
        formulation and reform.<br>
        <br>
        The Arab World Competitiveness Report series serves as a platform for 
        public-private dialogue on issues related to competitiveness. These 
        high-level discussions use the findings of this report as a basis for 
        competitiveness benchmarking and for advancing policy discussions.<br>
        <br>
        Klaus Schwab, Founder and Executive Chairman of the World Economic 
        Forum, said: “Although the region’s economies are currently very dynamic 
        and offer tremendous business opportunities, there is no doubt that 
        improvements to national competitiveness and closer integration with the 
        global economy and within the region are necessary if this growth 
        momentum is to be sustained.”<br>
        <br>
        Stressing the importance of education reforms, Sherif El Diwany, 
        Director, Middle East , World Economic Forum, said: “Entrepreneurship, 
        an element that is often cited as the key to unlocking the potential of 
        the Arab economies, can only take root in societies where freedom of 
        thought, enthusiasm for inquiry and critical thinking are popular 
        values.”</font></p>
