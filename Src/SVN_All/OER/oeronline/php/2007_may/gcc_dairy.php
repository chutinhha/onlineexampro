        Anytime-Anywhere Internet</b><br>
        <img border="0" src="gcc2.jpg" width="175" height="167" align="right" hspace="6" vspace="4">The Riyadh Development Authority switched on the WiMax transceivers on 
        Tahlia Street in downtown Riyadh, marking the first stage of its ‘Smart 
        City’ programme.<br>
        <br>
        The new wireless street is part of Riyadh City’s ‘Smart City’ programme, 
        which has already brought wireless internet to the city’s main coffee 
        shops and hotels. The wireless system begins working when someone turns 
        on a computer and connecting is free of charge. A Wi-Fi enabled laptop 
        or other wireless device automatically picks up the signal sent from 
        WiMAX hubs along the street and gains access to the system when the user 
        agrees to the terms of one of the web sites.<br>
        <br>
        Said Mohammed Saqer, Chief Executive Officer, ITC, “It is now possible 
        for anyone with an Intel Centrino notebook or other wireless enabled 
        device to seamlessly connect to the internet from anywhere on Tahlia 
        Street between Olaya Street and Dhabab Street.”<br>
        <br>
        “This installation of free wireless on Tahlia Street is a good example 
        of how a public-private partnership should work,” said Ferhad Patel, 
        General Manager, Intel Corporation, Saudi Arabia.<br>
        <br>
        The service, sponsored by Intel Corporation, the Communications and 
        Information Technology Commission (CITC) and the Riyadh Development 
        Authority, seeks to revitalise communities through the WiMAX technology.<br>
        <br>
        “With a longer range than the popular Wi-Fi wireless standard, WiMAX-enabled 
        laptops and portable devices will give mobile workers even more freedom 
        to get a fast and affordable internet connection wherever they are,” 
        added Patel.<br>
        <br>
        The service can also benefit local businesses that already exist on the 
        Tahlia Street WiMax strip. By investing in wireless technology, offices 
        within the zone will be able to make full use of the WiMax signal to 
        connect to the internet and download data.</font></p>
