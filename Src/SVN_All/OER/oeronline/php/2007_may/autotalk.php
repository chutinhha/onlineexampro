<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">X Means Exhilaration </font></b>
<font face="Verdana" size="1"> <br>
<b>The new BMW X5 smoothly combines dynamic driving capabilities, luxury and 
impeccable technology. A test drive report by Anne Kurian</b><br>
<br>
<img border="0" src="car1.jpg" align="left" hspace="6" vspace="4">When it comes to innovation, BMW never takes a backseat. In fact, many a time, 
the German luxury automobile company takes the lead. One example of BMW’s 
commitment to do things differently is the creation of the new segment of Sports 
Activity Vehicle. They launched the X5 a few years back under this segment and 
it proved to be a crowd puller right from the beginning. If figures are to be 
believed, BMW has sold 580,000 units the world over. <br>
<br>
With the X5, BMW provided the consumer with versatile driving options, luxury 
and superior technology. In the latter part of 2006, BMW launched the second 
edition of X5 in the West and last month, this was launched in Oman. Enhanced 
driving dynamics, exclusivity and improved technology feature prominently in the 
new X5. We were part of the press fraternity that tested the BMW X5. An 
enlightening and exhilarating experience it proved to be!<br>
<br>
<b>On the road<br>
</b>The test drive selected was Muscat- A’Roos in Jabal Akhdar-Al Hoota Cave-Muscat. 
At The Chedi, which was our starting point, we found that the sleek and muscular 
lines of the X5 gave it a lean, mean look, as if silently letting us know that 
it was game for any adventure. <br>
<br>
<b>
<img border="0" src="ca2.jpg" width="300" height="202" align="right" hspace="6" vspace="4"></b>When BMW first launched the X5 in 1999, one of its unique features was luxury 
combined with all-road performance. In its second edition, BMW maintains the 
features but also heightens the driving experience with impressive functioning. 
Our first experience of the X5’s smooth and easy manoeuvring was on the highway 
to our first destination, A’Roos Mountain. Gliding, the X5 rode the tarmac 
effortlessly and we had to be careful not to speed. Sitting in an X5, it was 
hard to determine the speed we were going at without checking the speedometer 
regularly. Blame it on exceptional driving dynamics! <br>
<br>
En route to A’Roos we came across a couple of curves where we put to test the 
X5’s ability to handle them at high speed. We had been briefed about the X5’s 
new unique wishbone front axle, which increased driving dynamics relating to 
speed, road handling and directional stability. Sure enough, the SAV executed a 
neat turn on the curves without letting speed hamper the performance. This is 
one vehicle where you are sure to feel in complete control behind the wheel. The 
road to A’Roos after the police checkpoint was a steep climb complete with sharp 
curves. BMW couldn’t have picked a better path to demonstrate the X5’s road 
handling. Here again, the X5 proved the effectiveness of the double wishbone 
axle as the Bimer sailed past the sharp bends. Directional stability at top 
speed is one of the high points of this SAV. <br>
<br>
<b>
<img border="0" src="car3.jpg" align="left" hspace="6">Technology at its best<br>
</b>After a short break at A’Roos, it was time to hit the roads to our next 
destination – Al Hoota Cave. Downhill this time, it meant even greater caution 
while pursuing high speeds. The road brought to fore another unique feature of 
the X5 – Active Steering with Servotronic. With this feature, the X5 helps in 
optimum steering control and improved driving constancy. Active Steering helps 
stabilise the vehicle if one oversteers by countersteering the X5 swiftly. The 
countersteering effect even helps when braking suddenly by preventing the 
vehicle from veering out of control. <br>
<br>
The descent downhill was enlivening as the X5 raced past the bends with little 
effort. The X5’s Dynamic Stability Control ensured that the descent was safe and 
there was little chance of going out of control. The cruise control function 
comes with a brake feature, which reduces road speed whenever necessary under 
particular driving conditions, and this we engaged on the Nizwa road to Al 
Hoota. This feature. <br>
<br>
On the trip back from Al Hoota to Muscat, we shifted from the automatic 
transmission drive mode to manual gearshift. If we were impressed with the 
six-speed automatic transmission, the manual gearshift had us floored. Shifting 
between gears was swift and smooth, while the commendable brake system kept the 
vehicle in control even as we traversed through various speeds and road 
conditions.<br>
<br>
<b>Overall impressions<br>
</b>The interior of the X5 embodies sheer luxury, one of the determining features 
being its uncluttered and aesthetically designed cockpit. The new X5 also has a 
third row of seats, which can be pulled down when more storage space is 
required. Some of the other features that impressed us in the X5 were the 
automatic air conditioning, the ever famous iDrive Control System that 
facilitates operation of functions like AC control, entertainment, navigation 
and communication, and MP3 compatible CD player. <br>
<br>
The X5 is a clear winner in its category. While it takes driving experience to 
newer heights, the technological advancements in the new X5 makes it an enduring 
vehicle. Luxury, superb road handling and futuristic technology functions 
reinforces the BMW X5’s position as a forerunner in the 
SAV segment. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
