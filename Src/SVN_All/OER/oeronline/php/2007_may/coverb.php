All figures are rounded off.<br>
<br>
<b>Profits<br>
</b>Profits are shown after taxes and all charges including extra-ordinary charges. 
Figures in brackets indicate a loss. All losses and negative growth are also 
ranked where possible.<br>
&nbsp;<br>
<b>Assets<br>
</b>Assets shown are as per the balance sheet at the end of the year. It is the 
total of Fixed as well as the current assets.<br>
<br>
<b>Shareholders Equity</b><br>
Shareholders Equity is the paid up capital of the company, retained earnings, 
and statutory and all other reserves as well as share premium.<br>
<br>
<b>Market Cap<br>
</b>Market Capitalization figure has been arrived at by multiplying the total number 
of outstanding shares of the company by the price per share as of close of 
business on 2 April 2007.<br>
<br>
<b>Earnings per share</b><br>
The earnings per share are as declared by the company in its published financial 
statements.<br>
<br>
<b>Dividend Yield</b><br>
The dividend yield figure is calculated on the basis of dividend declared in the 
financial statements for 2006 against the share price at close of business on 31 
December 2006.<br>
<br>
<b>Price Earnings ratio</b><br>
This ratio has been calculated by dividing the earnings per share for the year 
2006 by the share price on 2 April 2007.<br>
