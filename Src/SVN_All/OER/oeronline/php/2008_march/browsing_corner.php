        </font><b><font face="Verdana" size="4">Brewing economic crisis</font></b><font face="Verdana" size="1"><br>
        <b>Stephen Leeb’s book warns that the impending rise in energy cost is 
        bound to escalate living costs thanks to our enormous dependence on 
        energy for growth</b><br>
        <br>
        <img border="0" src="browse.jpg" width="168" height="302" align="left" hspace="8" vspace="6">Planet Earth has certain human specimens who gloat over their 
        ‘predictability’ faculty. They rejoice immensely when their utterances 
        turn out to be true. This ‘I-told-you-so’ community of writers generate 
        some mirth, some respect and some irritation. Yes, some admiration in 
        certain sections of society. <br>
        <br>
        Stephen Leeb’s book, The Coming Economic Collapse: How You Can Thrive 
        When Oil Costs $200 a Barrel, co-authored with Glen C Strathy, falls 
        into that category. I picked up the book on the day (February 20) global 
        oil prices closed above the US$100 a barrel mark on the New York 
        Mercantile Exchange, thanks to 100-odd futures traders booking orders at 
        that price. Astronomical? Nope. “My prediction of oil at $100 a barrel, 
        which many found so outlandish in 2004, now seems well within reach,” 
        crows Leeb in his preface. <br>
        <br>
        Brought out by Warner Business Books, originally in 2006, it came out 
        with the first trade edition in February last year. Well, that time oil 
        was hovering around $90 and did not pierce the lowest three-digit 
        denomination. Now it has. That is the big difference. Leeb refuses to 
        take a ‘bow and quit’. Know why? For him the oil situation is much more 
        important an investment theme. It is evolving into, according to Leeb, 
        ‘a civilisation-threatening crisis’. Verbal bombshell.<br>
        <br>
        Leeb takes readers through the past energy crises and how governments – 
        for him, the US mandarins – handled them. A rise in energy cost is bound 
        to escalate the living cost thanks to our enormous dependence on energy 
        for growth. When it is coupled with the refusal to moderate energy 
        consumption, the highly susceptible supply and demand mismatch is bound 
        to take its toll. <br>
        <br>
        What we witness today is that outcome of this relentless race between an 
        increasing number of buyers and limited supply or suppliers. The 
        “Chindia” factor is a case in point. The scorching pace of economic 
        development the two giant Asian neighbours are posting for the past few 
        years necessitates an unstoppable demand for oil and thus poses a severe 
        drain on available resources.<br>
        <br>
        On the crucial aspect of investment, Leeb paints a gloomy picture for 
        ordinary investors – read American again – if they follow the past 
        pattern of blindly investing in mutual funds with a strong belief in 
        equity growth. “…While some investors will grow incredibly wealthy, the 
        majority will experience a great deal of disappointment,” he writes. 
        Rising inflation and stagnant growth cut into investible resources and 
        lead to unemployment. Not an enticing scenario. Add outsourcing to the 
        hot pot and you have a brewing economic crisis. <br>
        <br>
        So, how to beat this uncertainty? “In a high inflation climate, 
        investors cannot be satisfied with the growth that is merely good. Nor 
        can they gain any safety from tracking the broad market. To beat 
        inflation, they need to focus their investment capital on stocks where 
        earnings growth is exceptional,” advises Leeb. What is the survival 
        strategy? Three options are considered: back to farm-based economy; 
        zero-growth state; scouting for alternate energy source. If you are an 
        investor, you are already aware of alternate wealth creation and 
        preservation routes to beat inflation and ride high. So Leeb offers 
        nothing new. Except some John Grishamish page-turner thrills. Surely, a 
        nice pick for long waits at airports when stranded due to whatever, you 
        know!<br>
        <br>
        <b>- Ramesh Kumar<br>
