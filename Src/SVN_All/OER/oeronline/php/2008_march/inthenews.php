</font><b><font face="Verdana" size="5">Oman Air</font><font face="Verdana" size="4"><br>
undergoes personality revamp</font></b><font face="Verdana" size="1"><br>
<b>Oman Air has changed its logo and personality to transform into a globally 
competitive airline taking Oman brand to the world<br>
</b><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img border="0" src="inthenews2.jpg" width="400" height="181"><br>
<br>
In its quest to become a leading player in the global aviation industry, the 
Sultanate’s national carrier Oman Air, has undertaken a major brand personality 
revamp exercise. As the first step, the airline has changed its logo. Launched 
in a glittering ceremony at Barr al Jissah, the new logo — a gold and silver 
swirl — represents the contemporary rendition of frankincense smoke. Much sought 
after by the world since ancient days, Omani frankincense is one of the most 
powerful and evocative expressions of the nation’s vibrant history, culture, 
heritage and people.<br>
<br>
Ziad bin Karim al Haremi, Chief Executive Officer of Oman Aviation Services 
said, “The new colours of Oman Air reflect our journey of successful growth as a 
dynamic airline reaching out to the world. They mark our transition from a 
leading regional airline to a competitive global airline. Gold represents 
excellence and achievement and embodies the timeless traditions and the 
bountiful natural wealth of Oman. Silver represents the enduring and vibrant 
heritage of Oman, which has been reflected in intricate and exquisite silver 
artifacts such as the Khanjar, jewellery and more.”<br>
<br>
Continuing further, he said, “Symbolic of the infinite blue of the sky and sea, 
Turquoise Blue denotes the legendary hospitality and welcoming nature of Oman to 
travellers since ancient days. The colour also refers to the renowned seafaring 
heritage of the Omani people. The typography set in gold and silver hues 
signifies a dynamic and ambitious airline that is spreading its wings to achieve 
greater success. The bold and compact fonts denote stability, strength and 
synergy. The logo hence symbolizes the true essence of the Sultanate of Oman – 
the timeless traditions, enduring heritage and legendary hospitality”. He added, 
“As the ever-soaring wings of Oman, we will continue serving the nation with 
pride and passion while flying customers with exemplary standards of warmth, 
comfort and hospitality.”<br>
<br>
Talking about the airline’s recent achievements and future course of action, he 
informed, “Oman Air is committed to create more job opportunities for Omani 
nationals. True to its mission of giving wings to deserving young Omanis, we 
have recently recruited 15 Ab-initio Pilot Trainees. In 2007 alone, we launched 
nine new destinations taking our destination count to 25 cities around the 
world”. He said, “By the end of 2011, our fleet will grow to 19 aircraft 
comprising 10 Boeing 737-800s and 9 Airbus A330s. We will also be acquiring 6 
Boeing 787 Dreamliner aircraft beginning from 2012.”</font></p>
