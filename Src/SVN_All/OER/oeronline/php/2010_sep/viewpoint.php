                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    Real estate: Survival and growth</font></b><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2"><br>
				The financial crisis has highlighted the need for companies to 
				develop a plan of action to capitalise on opportunities<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				The In the current global economic environment, real estate 
				investors, owners, <br>
				<br>
				developers and builders worldwide are primarily focused on two 
				issues:<br>
				<br>
				Managing their companies through the recession <br>
				<br>
				Positioning their companies for future growth as global 
				economies and property markets start to recover<br>
				At the beginning of 2009, the outlook for global property 
				markets was bleak. Though there have been some bright spots 
				since then, the current climate is decidedly mixed.<br>
				<br>
				</font><b><font face="Verdana" size="2">Two classes of companies</font></b><font face="Verdana" size="1"><br>
				<br>
				The recession has had the effect of separating real estate 
				companies into two classes. In one class are companies that, at 
				the first signs of the recession, had the foresight to refinance 
				their short-term debt with longer term borrowings at modestly 
				higher rates. The other class comprises companies that were 
				slower to react. By the time, these companies tried to refinance 
				or were forced to do so because of debt maturing, banks had 
				tightened loan requirements.<br>
				<br>
				</font><b><font face="Verdana" size="2">A need to take action</font></b><font face="Verdana" size="1"><br>
				<br>
				The credit crisis and global recession have highlighted the need 
				for companies to develop a plan of action to capitalise on 
				opportunities, address the risks in their investment portfolios 
				and operations and position their organisations to grow and 
				compete in global property markets. Companies with strong 
				balance sheets and access to capital are considering whether to 
				buy distressed commercial properties in select global property 
				markets. Conversely, highly leveraged companies are preoccupied 
				with crisis management. Regardless, these companies also need to 
				look ahead and plan for the future of their organisations in the 
				global real estate market that will emerge from the recession – 
				a market in which developers, investors and lenders will be more 
				attuned to risk.<br>
				<br>
				</font><b><font face="Verdana" size="2">Accessing liquidity</font></b><font face="Verdana" size="1"><br>
				<br>
				The recession has refocused companies on the need to have a 
				strong liquidity position: to hedge against a future liquidity 
				squeeze or other contingencies and to accumulate cash for future 
				investments or other purposes. Many companies are trying to 
				increase their liquidity through various means.<br>
				<br>
				</font><b><font face="Verdana" size="2">Managing cash flows</font></b><font face="Verdana" size="1"><br>
				<br>
				Real estate executives spend most of their time fire-fighting: 
				protecting assets, controlling costs, and, most importantly, 
				managing cash flow. Some real estate companies are 
				stress-testing their cash flows, i.e., creating different 
				scenarios and analysing the effects on cash flows. Others are 
				changing their cash flow models and creating or improving 
				systems that warn of potential cash shortfalls.<br>
				<br>
				</font><b><font face="Verdana" size="2">Refinancing debt</font></b><font face="Verdana" size="1"><br>
				<br>
				To date, lenders globally have generally preferred not to 
				foreclose on loans and assume ownership of the collateral (Real 
				Estate Owned – REO). How long lenders will continue to keep 
				delinquent loans and REO on their books is uncertain. Banks 
				could come under pressure from regulators to write down and sell 
				problem loans and dispose of REO. One result of lenders 
				disposing of problem assets is that a flood of properties could 
				come on a country’s market, further&nbsp; depressing prices.<br>
				<br>
				</font><b><font face="Verdana" size="2">Re-evaluating risk 
				management</font></b><font face="Verdana" size="1"><br>
				<br>
				If there is one lesson that companies have learned, or should 
				have learned from the recession, it is that sound risk 
				management is key. Companies are re-evaluating their risk 
				management policies and practices, beginning with an analysis of 
				their track record in risk management.<br>
				<br>
				</font><b><font face="Verdana" size="2">a hard look at costs</font></b><font face="Verdana" size="1"><br>
				<br>
				Real estate companies are seeking to improve their performance, 
				and that begins with more effective cost management and greater 
				operational flexibility. On the asset side, companies are 
				conducting detailed analyses of the ability of assets to 
				generate cash flow and appreciate in value. In the process, they 
				are determining which assets to hold and which to sell (when 
				market conditions are right).<br>
				<br>
				</font><b><font face="Verdana" size="2">Rethink of business 
				model</font></b><font face="Verdana" size="1"><br>
				<br>
				When global economies were booming, real estate developers and 
				owners tended to focus less on operational or administrative 
				issues. But the recession has forced them to take a hard look at 
				their business models and consider whether to make changes. With 
				global investment activity picking up, international investors 
				are seeking to form more alliances or partnerships with local 
				partners to leverage their local knowledge and expertise. The 
				recession has caused real estate company owners and principals 
				to become more active in the management of their businesses.<br>
				<br>
				</font><b><font face="Verdana" size="2">Managing investor 
				expectations</font></b><font face="Verdana" size="1"><br>
				<br>
				Companies have to manage the expectations of investors, who were 
				accustomed to realising very high returns in boom times, but who 
				now may have to accept lower returns as some companies focus on 
				lower yielding but lower risk investments. In boom times, the R 
				word was ‘Return’; now it’s ‘Risk’.<br>
				<br>
				</font><b><font face="Verdana" size="2">Long-term growth</font></b><font face="Verdana" size="1"><br>
				<br>
				The slowdown in development and transaction activity in the 
				recession has given real estate executives some breathing room 
				and enabled them to reflect on their organisations. Extending 
				their company’s market reach, accelerating decision-making and 
				strengthening their management capability are among their top 
				priorities.<br>
				<br>
				</font><b><font face="Verdana" size="2">New opportunities</font></b><font face="Verdana" size="1"><br>
				<br>
				One of the most challenging questions for real estate executives 
				is how to build sustainable organisations that will continue to 
				adapt and grow in an increasingly global and competitive real 
				estate market. The global recession has taught executives what 
				haven’t worked for their organisations, and now they are focused 
				on what will work. Their attention is on solid, long-term 
				growth, not financial engineering.<br>
				<br>
				</font><b><font face="Verdana" size="2">Recruiting, retaining 
				talent</font></b><font face="Verdana" size="1"><br>
				<br>
				Real estate CEOs are evaluating the talent they have, and the 
				talent they will need to support future growth. Companies now 
				recognise the importance of having people with deep and tested 
				real estate experience and skills. Given the market downturn, 
				there may be many strategic hiring opportunities – at the right 
				price – for companies with the resources to invest.<br>
				<br>
				</font><b><font face="Verdana" size="2">Looking ahead</font></b><font face="Verdana" size="1"><br>
				<br>
				The recession has been painful for the real estate industry. It 
				has tested the mettle of executives as never before as they 
				grapple with the shifting landscape. For those with capital 
				availability, the top priorities include: seizing opportunities; 
				addressing risks in investment portfolios and operations; and 
				positioning for growth. For those still struggling to survive, 
				the focus is on liquidity and cost-cutting.<br>
				<br>
				Regardless of their market position, companies need to look 
				ahead and plan for the future. Executives with the fortitude to 
				make the bold moves will position themselves well to build 
				sustainable organisations that will continue to adapt and grow 
				in an increasingly global and competitive real estate market.<br>
				<br>
				We are entering a new and changing world. Executives, who show 
				ingenuity, have the courage to make tough decisions and 
				demonstrate the foresight to apply lessons from change will 
				guide their companies to success in the real estate sector. And 
				they will be the leaders who establish the foundation upon which 
				our new global economy will rise.</font><hr color="#C0C0C0" size="1">
