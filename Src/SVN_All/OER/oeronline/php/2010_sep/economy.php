                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					Thinking Ahead</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<br>
					<b><font face="Verdana" size="2">
					<img border="1" src="images/sep_oliver.jpg" align="right" hspace="10" style="border: 1px solid #9A9A9A" width="100" height="94">EOR 
					techniques gave a boost to the hydrocarbon production of 
					Oman leading to a healthy energy sector</font><font face="Verdana" size="1"><br>
					</font>
					</b><font face="Verdana" size="1"><br>
					Since 1947, a team of scientists at the University of 
					Chicago has maintained a ‘Doomsday Clock’: a symbolic 
					representation of how close the world is to a history-ending 
					catastrophe. Based on the consensus of experts, the time on 
					the clock is changed periodically, with every minute closer 
					to midnight signifying a step in the direction of a global 
					disaster. The hands of the clock can be moved in both 
					directions: the closest it has ever come to midnight was in 
					1953, when the Soviet Union and the US tested nuclear bombs 
					within nine months of each other; the furthest was in 1991 
					when the US and Soviet Union signed the Strategic Arms 
					Reduction Treaty.<br>
					<br>
					For generations, the Doomsday Clock translated the 
					complicated scientific jargon and alarming media coverage of 
					the Cold War into a simple, compelling statement. Each step 
					closer to midnight was terrifying, but also proved to be an 
					effective way to raise public awareness and rally calls for 
					nuclear disarmament. <br>
					<br>
					</font><b><font face="Verdana" size="2">Future sans petrol<br>
					</font></b><font face="Verdana" size="1"><br>
					The importance and recognition of the Doomsday Clock in 
					popular culture has receded somewhat since the end of the 
					Cold War. However, imagine, for a minute, if a body of 
					experts convened to produce a symbolic representation 
					estimating when hydrocarbon reserves would run out. <br>
					<br>
					This will be no doomsday, to be sure, as the ever-growing 
					roster of renewable energy sources promise that the human 
					race will be able to survive, somehow, without 
					diesel-powered SUVs. However, the day when the last gas 
					plume peters out will mark a sea change in our ability to 
					conduct certain energy-intensive activities, from air travel 
					to chemical refining, which are indispensible to our current 
					way of life. <br>
					<br>
					As some groups focus on preparing for a post-petroleum 
					future, others are developing innovative ways to extract the 
					most from existing oil and gas wells. Oman has become a 
					centre for just this sort of innovation. The 
					government-owned Petroleum Development of Oman (PDO) has 
					been focusing on development in the fields of enhanced oil 
					recovery (EOR) and natural gas extraction for almost 30 
					years.<br>
					<br>
					</font><b><font face="Verdana" size="2">EOR exuberance<br>
					</font></b><font face="Verdana" size="1"><br>
					Oman’s earliest EOR experimentation began with projects in 
					hot water injection at the Qarn Alam field in the 1980s, 
					while polymer injection was tested at the Marmul facility. 
					These initial efforts, and follow-up projects in the 1990s, 
					gained new potency when Oman’s oil output entered a decline 
					– thought by many to be irreversible – after a peak in 2001.<br>
					Using traditional methods, primary and secondary recovery, 
					the quantity of crude that can be extracted from an oil 
					field is generally between 20 per cent and 40 per cent of 
					the oil it contains. EOR solutions enable companies to reach 
					more of the oil in a well – at the moment, EOR technology 
					enables extractors to extract between 30 per cent and 60 per 
					cent of estimated reserves.<br>
					<br>
					While the techniques under development vary widely, they are 
					based on two principles that enable further extraction. The 
					first is the introduction of gases to the well that change 
					its pressure, enabling companies to force further oil out of 
					its confined space. The second principle involves affecting 
					the oil itself, introducing a change to its viscosity that 
					enables it to flow more easily at lower pressures. <br>
					<br>
					</font><b><font face="Verdana" size="2">Early EOR 
					experiments</font></b><font face="Verdana" size="1"><br>
					<br>
					The first exploration into EOR techniques came from well 
					beyond Omani shores: the Kern River field in California was 
					the site of the earliest EOR extraction, where engineers 
					introduced steam to the well in an effort to heat the oil in 
					1964. Known as thermal extraction, this technique affects 
					the oil’s viscosity and allows it flow more easily. The next 
					breakthrough came in 1977, when engineers at the Alaskan 
					Prudhoe Bay field injected natural gas into a dwindling oil 
					field.<br>
					<br>
					Variations on this method have so far been the most popular 
					method of extraction, as the gas reduces the viscosity of 
					the oil while increasing pressure within it, facilitating 
					the oil’s rise to the surface. Other methods – including the 
					use of chemical solvents and microbes to reduce the 
					resistance of the oil to flowing - have traditionally been 
					seen as too costly or complicated for widespread use.<br>
					<br>
					Yet recent developments in Oman have shown marked progress 
					in these lesser-explored methods of extraction. In 2005, PDO 
					signed an agreement with Petrofac to develop its Harweel 
					facilities and enable the use of miscible gas injection, a 
					process whereby natural gas is blended with oil to reduce 
					its viscosity. The project brought some 18,000 bpd of 
					production on-line in phase one, and the second phase is 
					expected to be complete this year. Meanwhile, the Marmul 
					field is seeing the expansion of its polymer injection 
					process, which is anticipated to bring an additional 8000 
					bpd to production.<br>
					<br>
					</font><b><font face="Verdana" size="2">Record setting</font></b><font face="Verdana" size="1"><br>
					<br>
					The Qarn Alam field is also being transitioned by the PDO 
					from its early experiments into the world’s first full-field 
					steam injection EOR project, and could see a jump from 1,200 
					barrels per day to 30,000 for the next three decades. The 
					project is expected to be fully operational from the second 
					quarter of 2011. Occidental Chemical has been developing the 
					Mukhaizna field since 2004 and anticipates that its steam 
					thermal EOR project will enable it to produce 150,000 bpd by 
					2012, up from its present 100,000. At the Amal fields, 
					earlier developments are also being rolled into a steam 
					injection process, set to be complete in 2012.<br>
					As a result, there are currently five major EOR projects 
					under way. The return has so far been impressive: oil 
					exports grew 7.4 per cent in 2009 and then 8.7 per cent in 
					the first quarter of 2010. Oman’s minister of oil and gas, 
					HE Mohammed bin Hamad Al Rumhy, told the press in April that 
					production is expected to increase to between 850,000 and 
					900,000 bpd in 2010.<br>
					<br>
					</font><b><font face="Verdana" size="2">Gas support</font></b><font face="Verdana" size="1"><br>
					<br>
					The energy necessary for generating heat for these projects 
					comes largely from natural gas, and these projects have 
					played a part in increasing the domestic demand. In February 
					2010, the government announced that it would hold much of 
					its gas production for use on local projects. The 
					announcement comes despite a number of developments set to 
					increase production levels, which stood at 85m cu ft in 
					2009. In addition to newly-discovered gas reserves at Khulud, 
					BP is expected to bring its Khazzan and Makarem fields 
					on-line soon. A test site at the Khazzan field yielded 40m 
					cu ft per day, and the company estimates that some 1m cu 
					metres could be produced there before the end of 2010. The 
					fields are estimated to contain a total of 30trn cu ft of 
					gas.<br>
					These recent developments in the gas segment are set to 
					support innovations in oil extraction. Though not a 
					permanent solution, as oil and gas resources are still 
					finite, the ability to utilise EOR to obtain petroleum has 
					enabled Oman to revitalise its energy sector.<br>
					<br>
