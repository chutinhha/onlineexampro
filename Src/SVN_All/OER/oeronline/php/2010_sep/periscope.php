					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					What next for the India Fund?<br>
					</font><font face="Verdana" size="2"><br>
					Rise in savings and massive infrastructure spending will 
					reduce India’s dependence on exports and keep its economy 
					robust<br>
&nbsp;</font></b></p>
					<p align="center" style="margin: 3px 10px"> 
				<b>
					<font face="Verdana, Arial, Helvetica, sans-serif" size="2" color="#CC3300">
				<img border="2" src="images/sep_periscope1.jpg" hspace="9" style="border: 1px solid #808080; " vspace="12"></font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">While the Western economies 
					contracted in 2009, India emerged from the global credit 
					crunch with minimal damage to its financial system, though 
					the Sensex plunged 12000 points and ICICI Bank needed an RBI 
					lifeboat once the offshore money markets blackballed its 
					banking subsidiaries in Canada and Britain. While Indian GDP 
					growth rates and industrial production fell, the 
					deceleration was both relatively modest and temporary. 
					Unlike China, the ruling Congress Party did not face the 
					loss of 40 million factory jobs and social unrest once 
					export markets in the US and Europe collapsed. Nor was India 
					forced to resort to a state-sanctioned trillion dollar bank 
					lending spree, as happened in China.<br>
					<br>
					However, the RBI eased interest rates by 450 points and New 
					Delhi expanded fiscal policies. The chickens have now come 
					home to roost as wholesale inflation has spiked above 10 per 
					cent after the government slashed fuel subsidies, the reason 
					the RBI has been forced to increase the repo rate at 
					successive monetary conclaves.<br>
					<br>
					</font><b><font face="Verdana" size="2">Robust economy</font></b><font face="Verdana" size="1"><br>
					<br>
					India is among the most resilient economies in Asia because 
					it is dominated by consumption and services, not exports to 
					the West and capex. However, exports are 24 per cent of the 
					Indian economy and Europe is the country’s largest trading 
					partner. This means Indian industrial production will remain 
					hostage to the global business cycle. However, the secular 
					rise in Indian savings to 36 per cent and Dr Manmohan 
					Singh’s $400bn infrastructure spending will reduce Indian 
					dependence on exports to the EU, its largest export market.<br>
					<br>
					This is the reason the post-crisis performance of the Indian 
					economy is so robust, with GDP growth rates near 9 per cent, 
					an economic surge that has reignited inflation and forced 
					the RBI to tighten credit. With high world food and crude 
					oil prices, more RBI tightening is inevitable. Apart from 3G 
					or asset sales windfalls, the budget will not reverse the 
					widening fiscal deficit trend bequeathed by the crisis.<br>
					</font><b><font face="Verdana" size="2"><br>
					Huge valuation premium<br>
					</font></b><font face="Verdana" size="1"><br>
					The Sensex rose a spectacular 80 per cent as financial 
					markets realised that India had dodged the post-Lehman 
					recession bullet spawned in Wall Street, the City of London 
					and Zurich. Dalal Street is a magnet for global investors as 
					risk appetites surged, sovereign credit default fears 
					vanished and India’s economic and earnings growth surged. 
					Indian GDP growth in the next three years will be almost 
					triple the anaemic growth in the US and Europe. This is the 
					reason India trades at a huge valuation premium to Western 
					markets or even the major emerging markets at 16 times 
					forward earnings.<br>
					<br>
					However, I believe that a 36 per cent valuation premium to 
					emerging markets is excessive at a time when the RBI will 
					hike interest rates, while inflation, high oil prices and 
					borrowing costs dampen both consumer spending and industrial 
					output. The reason Sensex traded at 18000 was only because 
					the mood swings of global investors veered from fear to 
					greed, with the Chicago Volatility Index falling from 40 to 
					22. The Indian economy may be somewhat immune to global 
					shocks but Dalal Street is still hostage to global liquidity 
					flows. Just because India’s GDP growth is not entirely 
					export dependent does not mean that the Sensex is not 
					vulnerable to a global investor loss of confidence.<br>
					Safe to trade<br>
					<br>
					At 18,000, the Sensex is overbought as it prices at least 25 
					per cent earning’s growth at a time when the central bank is 
					determined to lean against the wind and tighten interest 
					rates. Once the crude oil prices rise above $80, it is 
					entirely possible that Indian earnings growth projections 
					are far too bullish. This is particularly so as the UK and 
					Eurozone economies are most vulnerable to a double dip 
					recession.<br>
					<br>
					India is a secular global growth story and will trade at a 
					premium valuation even after a correction. However, I do not 
					think the Sensex will revisit the all time high at 21,077 on 
					January 2008. Nor will it fall to its 8,160 low in March 
					2009 when foreign money fled Dalal Street. A 15,000 – 20,000 
					range is a far more likely and safe trading range to own the 
					India Fund (IFN) listed on the New York Stock Exchange.<br>
					<br>
					The India Fund (symbol IFN) rose from 13 in March 2009 to 
					its current price in the 30-32 range. The India Fund, 
					managed by Blackstone Asia Advisors, has a heavy weighting 
					in Reliance Industries, Infosys, HDFC, ICICI Bank and TCS. 
					These shares alone constitute one third of the India Fund. 
					IT, finance and petrochemicals are all high beta, cyclical 
					industries. Therefore, I can easily envisage the India Fund 
					(IFN) falling to 24-25 if Sensex corrects down to 15,000. 
					The India Fund will offer value at 24-25 for a 35-37 target 
					in the next 12 months.<br>
