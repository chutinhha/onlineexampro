            </font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>Martina Starke, Head of Design, BMW Individual speaks about the 
            interior design of the new BMW 7 series. Visvas Paul D Karra reports<br>
            </b><br>
            <img border="0" src="passingby.jpg" align="left" hspace="8" vspace="6" width="300" height="253">It takes about six years to design the interiors of a BMW before the 
            actual production begins. It involves two clay models, four-five 
            designers who have to spend time finding ways to combine form, 
            functionality and design. Then the last two years are spent on 
            finalising the design. The end product should be describable in four 
            words – sportiness, elegance, modernity and presence.<br>
            <br>
            Design is a critical and overriding factor when it comes to a car’s 
            popularity and if it is a BMW, which epitomises elegance and class 
            of the owner, then certainly you need to talk the language of forms 
            which is best understood by an exclusive set of people who have a 
            taste for the finer things of life. It is no wonder that such 
            attention to detail is invested in the interior of a BMW.<br>
            <br>
            <b>An eye for design</b><br>
            If the latest BMW 7 series strikes you with its attractive and 
            enigmatic interiors, then the credit goes to Martina Starke, Head of 
            Design, BMW Individual, who has been with the premier carmaker for 
            the last eight years. Starke started her profession as a textile 
            pattern maker, where she first explored her great passion for 
            materials and textiles, Oman Economic Review caught up with Starke 
            during her brief visit to Oman to find out more about her role in 
            the latest BMW 7 series.<br>
            <br>
            Starke, has been an essential member of colour and material 
            department at BMW Design in Munich since 2001. She joined the 
            company as a colour, material and seam pattern designer. Three years 
            later, Martina took over the position of colour and material project 
            manager, her first lead project being the development of the BMW 3 
            Series Coupe and Convertible (E46) colour and material options. More 
            recent work includes colour and material design for models such as 
            the new BMW 7 Series and the BMW Concept CS. <br>
            <br>
            Currently, Starke is leading the colour &amp; material development at 
            BMW Individual, which offers both packages and bespoke colour and 
            trim combinations created for customer wishes.<br>
            <br>
            Says Starke: “The process of transforming natural materials such as 
            silk, cotton or leather through various steps into a textile with 
            great tactile feel and appearance that can be used in interior 
            spaces is a great fascination for me. This process has had shown a 
            compelling magnetism for me since early in my career and has become 
            an overall driving force.”<br>
            <br>
            Starke’s greatest sources of inspiration have been designers such as 
            Li Edelkoort and Angelo Donghia who not only challenged the 
            traditional way of thinking about textiles, but they have also 
            explored the possibilities of bridging tradition and modernity.<br>
            <br>
            <b>Exploring possibilities</b><br>
            Talking about her work, Starke informs that they design everything 
            in the interior, from the ceiling to the seats. Right from the 
            material to the stitching, everything is taken into account. We 
            develop for every car, a new colour concept that is based on the 
            factors that distinguishes it from others, she says. While crafting 
            the interiors, the design team also bears in mind the changing 
            styles and demands and incorporates many technical innovations like 
            the driver orientation and ceramic idrive options in the new BMW 7 
            series, for instance to convey a new interpretation of modernity. A 
            particularly eye-catching feature of the cockpit is the instrument 
            cluster with black panel technology.<br>
