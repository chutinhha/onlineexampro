                partly because the market has grown and partly because we have 
                strengthened our position. Al Habib is the only valuer with a 
                branch in Nizwa and Sohar. Obtaining the right valuation is very 
                important as bank loans running into millions of rials, are 
                based on such data. As the market grows there will be an 
                increasing demand for such professional services. <br>
