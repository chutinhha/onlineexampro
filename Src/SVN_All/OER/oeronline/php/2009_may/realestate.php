                <img border="0" src="real.jpg" align="right" hspace="8" vspace="6" width="100" height="116">We assist our clients such as banks, courts, heirs and 
                beneficiaries etc to obtain a correct estimate as to the current 
                market value of a particular property. Such valuation is 
                required for various reasons - the most important of which being 
                that banks and financial institutions lend against real estate 
                collateral and they need to be reassured of a property’s real 
                value before advancing a loan. Companies also value property for 
                balance sheet purposes and families need to know the exact value 
                of their assets to distribute amongst their heirs. Finally, both 
                a buyer and seller of a property require a completely 
                independent valuation of worth before negotiating a final 
                contract. <br>
                <br>
                <b>Methodology applied</b><br>
                Al Habib follows the International Valuation Standards. There 
                are three methods of valuing property. First is comparables – 
                this infers the price of an asset based on the value that a 
                similar property has fetched in the vicinity. For example, if 
                there were identical houses in an area, and one of them fetched 
                a certain value, then one can conclude that the other houses in 
                the area would be of a similar value. Comparable does not mean 
                identical but establishes a base figure for that type and size 
                of property. <br>
                <br>
                The second is the income method, people generally buy real 
                estate as an investment and expect to get a return. At any given 
                time the market fetches a particular yield (return on 
                investment). For example if the income from a building is 
                RO100,000 a year. Then one may say that it is worth ten times 
                that value and is giving a yield of 10 per cent.<br>
                <br>
                Depreciated cost is the third method employed to arrive at a 
                valuation. If a buyer is offered a property, he has a choice of 
                either purchasing it or alternatively buying some land and then 
                constructing a similar building upon it himself. The buyer may 
                be willing to pay a premium for the existing building because it 
                saves him the time and effort to build and hedges him against 
                any increase in construction costs. In boom times the sale 
                premium over cost is very high, while during a downturn it is 
                low or negative.<br>
                <br>
                The value of a property is arrived at by factorising in all 
                these variables. The process is methodical as the results of all 
                these three methods need to converge and if there is a variance 
                there should be justifiable <br>
                reasons for it. <br>
                <br>
                In most other companies there is a single person carrying out 
                valuations. At Al Habib we have a committee to do valuations 
                with representatives from our broking, renting and construction 
                divisions. They each bring an expert opinion to the table. One 
                needs to be very active in the broking, rental and construction 
                market to arrive at the correct comparable, income and cost 
                figure.<br>
                <br>
                As Al Habib has expertise in broking, renting (being the largest 
                renting agency in the Sultanate) and the construction business, 
                our professional valuations carry much credibility in the 
                market. <br>
                <br>
