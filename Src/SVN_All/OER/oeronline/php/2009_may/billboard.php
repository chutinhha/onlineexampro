                  first grass championship golf course on Thursday 30th April 
                  2009 at a launch event attended by senior Omani dignitaries, 
                  Government ministers, sports and media representatives. 
                  Anticipated to be the most unique and breathtaking golfing 
                  experience in the Gulf, it is set to be the dawn of a new 
                  sporting era for the Sultanate of Oman, placing the country on 
                  the regional and international golfing map. The event 
                  celebrated the opening of its first nine holes by the 
                  ceremonial first drive down the fairway struck by leading 
                  local golfer, Azaan Al Rumhi, who plays for the Oman National 
                  Golf team. Al Rumhi, who is currently based in Thailand, was 
                  flown in for the exclusive occasion. The nine holes course was 
                  opened to the public from 2 May 2009.<br>
