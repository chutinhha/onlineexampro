                  Excellency the Minister of Tourism, Dr. Rajiha bint Abdulameer 
                  bin Ali, announced at Marina Bandar Al-Rowdha the start of a 
                  brand new tourist attraction. The semi-submersible vessel Al-Khayran 
                  will take visitors on an ‘underwater tour’ of the coral reefs 
                  of Bandar Al-Khayran. Visitors will sit in air conditioned 
                  comfort 2 meters below the water line and watch fish, turtles 
                  and other sea creatures which inhabit the coral reef, through 
                  large viewing ports in the underwater hull. Passengers will 
                  embark in a fast transfer boat at Marina Bandar Al-Rowdha for 
                  the half hour trip to Bandar Al-Khayran where they will board 
                  the semi-submersible. They will then experience the wonders of 
                  the underwater world in air-conditioned comfort for about 1 
                  hour before re-embarking in the fast transfer boat for the 
                  return trip to the marina. The whole round trip will take just 
                  over two hours.<br>
