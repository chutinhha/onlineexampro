                  schools at Suwaiq. Through this sponsorship A’Saffa aimed to 
                  help educate the sultanate’s young ones about the effects of 
                  environmental degradation, and its effect on climate changes 
                  as a whole. Educational institutions such as Abutalha Al 
                  Ansari, Fatma Bint Anabi and Jabir Abdullah Al Ansari held the 
                  well put together ‘Environment Week’ in their respective 
                  schools. With the advent of environmental degradation, climate 
                  change is one of the greatest threats facing our planet today, 
                  A’Saffa’s prime aim in sponsoring this event has been to help 
                  young pupils understand the importance of preserving their 
                  natural environment, and increasing their social awareness and 
                  responsibility towards protecting the environment.<br>
