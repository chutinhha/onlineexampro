                  <img border="0" src="bill1.jpg" align="right" hspace="8" vspace="6" width="216" height="150">The 
                  Chief Executive Officer of Oman Telecommunications Company (Omantel) 
                  Dr. Amer Bin Awadh Al-Rawas said that the company is currently 
                  studying the creation of a special price for the use of the 
                  3.5G network by the deaf and dumb segment to enable them to 
                  interact with their peers in the Sultanate and abroad through 
                  video calls in sign language. In a statement to the press 
                  following patronising the celebration marking the 14th 
                  anniversary of the Oman Association for the Disabled, the 34th 
                  Arab Deaf Week and the Gulf Disabled Week held at the Omani 
                  Women’s Society in Muscat said, “Omantel Group looks after the 
                  disabled and individuals with special needs as part of its 
                  corporate responsibility. It supports their activities and 
                  programmes with the objective being to integrate them into the 
                  society and make them equal to other individuals of the 
                  society.”<br>
