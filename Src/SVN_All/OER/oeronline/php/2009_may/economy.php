				<b><font face="Verdana" size="4">The Name’s Bonds, Sovereign 
                Bonds</font></b><font face="Verdana" size="1"><br>
                <b>Bonds are a logical step in addressing dwindling external 
                finance sources and the reality or prospect of a budget deficit. 
                Considering the extraordinary economic conditions though, any 
                taboo on both bonds and deficits has no rightful place in 2009</b><br>
                <br>
                <b>
                <img border="0" src="eco.jpg" align="right" hspace="8" vspace="6" width="125" height="148"></b>As it does every year, Q2 heralded the arrival of spring and, as 
                if by seasonal cue, the GCC bond market awoke itself from a 
                quarter-long hibernation. Several governments have either 
                issued, or announced plans to issue, sovereign bonds of 
                considerable size, including Kuwait and Abu Dhabi, which had by 
                April made clear their intentions to release bonds over the 
                coming months. They follow in the footsteps of Bahrain and 
                Dubai, both of whom announced or issued bonds in February, and 
                Qatar, which issued bonds at the beginning of April. While Oman 
                has yet to make any official declaration, at the March gathering 
                of GCC central bank heads, H E Hamoud Sangour Al Zadjali, 
                Executive President of the Central Bank of Oman (CBO), suggested 
                bonds could be a possibility later in the year. Rather than 
                signs of weakness, the recent swathe of bond activity signals a 
                prompt response to challenges not seen in the region since the 
                recession of the early nineties.<br>
                <br>
                Leading up to 2008 the shariah-compliant, or sukuk, bond market 
                showed much promise and was viewed by many as the ‘next big 
                thing’ in the Gulf. Since then, the market has softened, due in 
                large part to doubt over the instrument’s true compliance with 
                Islamic law. Bahraini cleric Sheikh Taqi Usmani warned in late 
                2007 that as much as 85 per cent of sukuk on the market were not 
                halal due to the sharing of risk. Overall, the aggregate bond 
                market, which includes both conventional and sukuk bonds, showed 
                a year-on-year decrease of 62.4 per cent to $18.1bn in 2008.<br>
                <br>
                <b>The gold rush </b><br>
                Bonds have been a relatively little-used financial instrument on 
                the part of both governments and local corporations, in large 
                part because until very recently they were not necessary in a 
                region flush with capital and hydrocarbon wealth.<br>
                <br>
                <img border="0" src="eco1.jpg" align="left" hspace="8" vspace="6" width="225" height="317">The latest raft of sovereign bonds is notable for two reasons. 
                First, it is yet another indication of the troubled state of the 
                world economy (as if anyone could forget). A five-year bull run 
                on oil left the GCC with a fiscal surplus estimated at 30 per 
                cent of GDP in 2008. Now, as international credit lines wither 
                and revenues decline on the back of lower oil prices, most of 
                these same states are looking for project financing and face the 
                possibility of posting a deficit – a novel problem for most Gulf 
                states in recent years.<br>
                <br>
                Interestingly the bonds received a spirited reception on the 
                world market, in a sign that risk appetite may be returning. The 
                Central Bank of Kuwait, for instance, received KWD1.261bn worth 
                of bids for an announced tender of KWD200m ($680.7mn) worth of 
                one-year treasury bonds. Such exuberance reflects positively on 
                the GCC, which is perceived as a bastion of relative stability 
                amidst the enfeebled global economic landscape of 2009. Bonds 
                are a logical step in addressing dwindling external finance 
                sources and the reality, or prospect of, a budget deficit. 
                Considering the extraordinary economic conditions though, any 
                taboo on both bonds and deficits has no rightful place in 2009.
                <br>
                <br>
                This year fiscal deficits are not only expected but also 
                “necessary,” as Marios Maratheftis, regional head of research at 
                Standard Chartered Bank told local press after several bond 
                announcements at the March central banks’ meeting. Indeed, from 
                the US to Japan, governments are adopting a Keynesian approach 
                to the crisis, aiming to ward off the worst of the recession 
                with generous state spending. The GCC is particularly well 
                poised to do this. As a point of comparison, the budget deficit 
                of the United Kingdom is predicted to near 13 per cent this 
                year, according to research by Ernst &amp; Young; most GCC states 
                are expected to register a deficit of around five per cent of 
                GDP.<br>
                <br>
                <b>Budget deficit</b><br>
                For now Oman has declined to jump on the sovereign bond 
                bandwagon, perhaps because it has maintained a relatively low 
                ratio of external financing. From 2005 to 2007 external 
                financing as a percentage of GDP was just below 20 per cent, 
                compared to over 45 per cent in Qatar and over 50 per cent in 
                Bahrain in 2007. The Sultanate is currently running a deficit of 
                about five per cent of GDP, or $2.1bn.<br>
                <br>
                The likelihood of Oman substantially adjusting its fiscal policy 
                in the near future is largely dependent on the price of oil. 
                Minister of National Economy, H E Ahmed bin Abdulnabi Macki, has 
                remarked on several occasions that should the price remain low, 
                officials may trim the $16.6bn (RO6.4bn) in public spending 
                pledged in the 2009 budget. Sovereign bonds could also provide a 
                way to bridge the financing gap. The Sultanate’s budget is based 
                on a per barrel oil price of $45. Depending on the measure used, 
                the record of the price of crude oil throughout 2009 is slightly 
                different, though no index produces a price chart comforting to 
                government officials of oil-exporting nations. <br>
                <br>
                <img border="0" src="eco2.jpg" align="right" hspace="8" vspace="6" width="227" height="220">According to the reference basket of 12 crude blends used by 
                OPEC (to which Oman is not party), oil was selling above $45 per 
                barrel (pb) for only 15 days since the start of this year. The 
                spot price of Oman crude on the Dubai Mercantile Exchange paints 
                a slightly rosier picture, with 23 days of oil priced at or 
                above $45, though only four of these days saw oil trading at $50 
                or more. At the time of Oman’s budget announcement on January 1, 
                an assumed oil price of $45pb looked somewhat cautious. Just a 
                few months later, some within the region were taking a more 
                conservative stance. Qatar has based its budget, which came into 
                effect on April 1, on $40pb while Sultan Nasser Al Suweidi, 
                central bank governor of the UAE recently said he does not 
                expect oil prices to average more than $40 to $44 this year. <br>
                <br>
                In spite of the new reality, Oman has to date announced no 
                high-profile project cancellations, nor has it made any serious 
                cuts to planned projects. In fact, the overall economic picture 
                for the Sultanate has remained essentially positive amidst the 
                gloom elsewhere. <br>
                <br>
                Moody’s latest annual country credit report, released in 
                mid-February, maintains a stable outlook for Oman’s A2 
                investment-grade sovereign ratings, citing the government’s 
                extensive offshore financial assets as a major cushioning factor 
                against the global credit fallout. Moreover the government’s 
                gross financial assets plus the central bank’s foreign exchange 
                reserves amounts to around 75 per cent of GDP and government 
                debt is low, said the report. <br>
                <br>
                Should the government go ahead with a bond issuance, the inroads 
                laid by its neighbours in the market will help develop a strong 
                base from which to launch. As sovereign bonds are a relatively 
                undeveloped mechanism in the region, recent issuances will begin 
                to generate a benchmark yield curve by which investors can more 
                accurately weigh risk and value – an especially important 
                mandate in the current cautious environment. <br>
                <br>
                <b>Companies get into the act</b><br>
                In Oman the private sector has already begun to release 
                corporate bonds, kick-starting the establishment of a widespread 
                debt market. On April 8, BankMuscat, of which the government is 
                a minority shareholder, announced it will launch a RO60mn 
                subordinated bond issue carrying eight per cent yield. As in the 
                past, the question of ‘conventional or sukuk’ will remain an 
                issue for consideration in both corporate and sovereign 
                issuances. While the tests of 2009 are not likely to be 
                remembered fondly by government leaders and investors, or anyone 
                for that matter, they do present the opportunity for the fiscal 
                tactics of all GCC states to mature as they attempt to manage 
                the twin challenges of declining revenues and increased public 
                spending. So far, it appears they are up to the test. <br>
