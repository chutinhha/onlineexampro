				<br>
				Absolutely Rocking</font></b><font face="Verdana" size="1"><br>
                <b>Volkswagen has finally revived its cult classic but does the 
                third-generation Scirocco live up to the family name? Malcolm 
                Xavier Crasta finds out<br>
                </b><br>
                <img border="0" src="autotalk2.jpg" align="left" hspace="8" vspace="6" width="400" height="266">Sometime during the early 1970s, Volkswagen began work on a car 
                that they internally designated as the ‘Type 53’. It had the 
                underpinnings of the Golf but was re-engineered to be far 
                sportier and the styling was penned by legendary automobile 
                designer Giorgetto Giugiaro. Soon after going on sale in 1974 
                the car, now called the Scirocco achieved cult status. This was 
                primarily due to the fact that the Scirocco was an affordable 
                sports car with everyday utility. A heavily re-designed “Mark 
                II” variant went on sale in 1982 and continued to do so until 
                its production stopped in 1992. Then finally, at the 2006 Paris 
                motor show, it was back in the form of the stunning IROC 
                concept. But, it was not until late 2008 that we got our chance 
                to see it in showrooms, and what a sight it was! From the front 
                it has a very aggressive and imposing design followed by a very 
                prominent character line running down the side of the low slung 
                vehicle and ends in a rather controversial, and quite wide, rear 
                end.<br>
                <br>
                <b>Roomy feel</b><br>
                Open the rather large doors and sink yourself into the very 
                supportive leather sport seats and you will find yourself right 
                at home. The seating position is just perfect and there is more 
                than enough articulation of the seats and flat-bottomed steering 
                wheel to make anyone comfortable regardless of their height. But 
                the same cannot be said about the rear passengers. While the 
                generous legroom is never an issue, unless someone really tall 
                sits up front, getting in and out can be really tricky because 
                of the low roofline. Aside from this the interior is the regular 
                fare most of us have come to expect from VW. The cabin is 
                tastefully designed, beautifully built and very user friendly 
                and those triangular door handles are a great touch. The touch 
                screen navigation and entertainment system is intuitive, 
                responsive and a breeze to master. Overall it is a great place 
                to be especially when you consider how devoid of wind noise it 
                is. Although the same cannot be said about the engine noise, it 
                can easily be excused simply because it just sounds so good.<br>
                <br>
                <b>Drawing parallels</b><br>
                On the road the Scirocco feels very much like a GTI, which is 
                great because the GTI is quite easily one of the best hot 
                hatches money can buy. This comes as no surprise when you 
                consider that the two share the same gearbox, have similar 
                engine output figures and weigh nearly the same. The 
                aforementioned gearbox is of the DSG (Direct Shift Gearbox) 
                variety. It is a dual-clutch sequential manual system and, just 
                like in the GTI, is an absolute dream to use. The shifts are 
                smooth and extremely fast and can even shift as fast as eight 
                milliseconds as long as the onboard computer (the ECU for tech 
                savvy people) is expecting it! <br>
                <br>
                <b>
                <img border="0" src="auto1.jpg" align="right" hspace="8" vspace="6" width="300" height="313"></b>The Scirocco is lower, slightly lighter and has a stiffer 
                suspension system which makes 
                it feel sportier and improves handling in exchange for some of 
                its ride quality. The steering is predictable, sharp and full of 
                feel. This is further complemented by its incredible high-speed 
                stability. Combine these and it instils a sense of confidence in 
                the driver that just seems to get better the faster you go, in a 
                way it spoils and coaxes you into driving faster. On the 
                equipment side you get the standard fare of equipment, which 
                includes leather, touch screen entertainment system with 
                navigation, bi-Xenon headlamps, 18” alloys and more. Even on the 
                safety front it follows the same trend and comes with all the 
                standard bells and whistles that cars in this class share such 
                as ten airbags, ABS, TCS, and ESP etc. So, to summarise 
                everything, the Scirocco looks good, drives great, is well 
                equipped and is reasonably priced. It is slightly more expensive 
                than a GTI and not as practical either but then again it is more 
                of a sports car than the GTI. As such it is slightly faster, 
                sharper and definitely handles and looks better. So, if you are 
                in the market for a good hot hatch then the Scirocco is a no 
                brainer but if you want a little more practicality then there is 
                always the GTI.<br>
