                conditions. Sunil Fernandes reports<br>
                </b><br>
                <img border="0" src="consumer.jpg" align="left" hspace="8" vspace="6" width="200" height="272">The 
                first question that comes to mind, when any top executive from a 
                global monolith visits Oman is: Is the global downturn affecting 
                your performance? For some executives the question may be 
                tricky; but not for H S Paik, President, LG Electronics Gulf. 
                “For the countries that I handle in the Gulf, I believe that the 
                first quarter will be better than last year. Oman is a country 
                that will witness growth. I have high respect for our partners, 
                OTE, and what they are doing in Oman,” he says. <br>
                <br>
                <b>Market leader</b><br>
                With a marketshare of 35 per cent in air conditioners, 40 per 
                cent in washing machines, and 30 per cent in appliances, LG 
                dominates the consumer durables market in Oman. Going forward, 
                Paik expects LG to continue to dominate the market and 
                consolidate its position. “With a continuous focus on innovative 
                products and a spate of launches, I believe we would be ahead in 
                the market place. We have proven that time and again, and would 
                do so in the future as well. We could be headed for double digit 
                growth here,” he says. An aggressive marketing strategy and a 
                constant focus on Research and Development(R&amp;D) have been 
                important attributes in LG dominating the market. Interestingly, 
                Paik believes that R&amp;D and marketing expenditure would continue 
                despite the global problems, while the company would look at 
                other areas to cut costs. <br>
                <br>
                “We will not decrease our investment in marketing or Research 
                and Development, despite a contraction in the global economy. If 
                we invest now, we will reap rich dividends when good times are 
                here, which will only help accelerate our progress. We currently 
                spend in excess of $1bn in R&amp;D and I only expect that to grow,” 
                he says. <br>
                <br>
                On areas that are seeing improved cost efficiencies, Paik points 
                to the supply chain management. “There is a potential to save on 
                the supply chain side. We are constantly striving to improve 
                cost efficiencies in this area. But allocation for R&amp;D and 
                marketing are going to only grow,” he says. <br>
                <br>
                While LG has been a leader in several of the businesses it 
                operates, there are also certain areas of business, that are 
                challenging and Paik would like to see grow. “We would like to 
                grow in the mobile phone market. LG is amongst the dominant 
                players in the CDMA segment, however, we would like to be 
                amongst the top three globally,” says Paik. <br>
                <br>
                As far as the Gulf market is concerned, there are certain areas 
                that have been affected by the global economic downturn. “On 
                average our performance in the Gulf is good, except for the 
                system air conditioning market in the UAE. We are the leaders in 
                the market there, and there is an adjustment that is taking 
