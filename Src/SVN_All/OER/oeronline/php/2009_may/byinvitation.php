                <br>
                </font><b><font face="Verdana" size="4">Oman: The fruits of a 
            modern Renaissance</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            Mohsin Aal Busaidi’s success with fellow crew members of ‘Mmusandam’ 
            circumnavigating the globe nonstop under sail is Symptomatic of a 
            mounting body of evidence that Oman is a nation “Whose time has 
            come”.<br>
            </font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="invitation.jpg" align="right" hspace="8" vspace="6" width="140" height="188"><br>
            Renaissance means ‘re-birth’. In Oman, we associate the term 
            ‘Renaissance’ with the period of outstanding change and development 
            that has galvanised the country since the accession of His Majesty 
            Sultan Qaboos bin Said in 1970. Oman’s Renaissance has witnessed 
            transformational positive change in all fields of life, including 
            education, health, commerce, security and infrastructure. One of the 
            truly remarkable achievements of this era is that the dazzling 
            acceleration of modern progress has been achieved alongside the 
            preservation and deepening appreciation of Oman’s rich heritage and 
            culture. Deep in the Omani psyche lies, not only a love of the 
            beauty and wilderness of the desert, but also a yearning for the 
            high sea. Throughout history, Oman has produced skillful seafarers 
            who have been the interlocutors for Oman’s exchanges of culture and 
            knowledge with the outside world and the perpetrators for trade and 
            prosperity through the ages.<br>
            <br>
            <img border="0" src="byinvitation1.jpg" align="left" hspace="8" vspace="6" width="300" height="222">That 
            is why the Oman Sail project is so important, with its aim to 
            rekindle Oman’s maritime heritage. At 15:43:12 local time (11:43:12 
            GMT) on March 25, 2009 Oman Sail’s 75-foot TrimararanMusandam 
            crossed the finish line in Muscat and Mohsin Al Busaidi became the 
            first Arab to sail non-stop around the world. As a member of a 5-man 
            crew, including skipper Loik Gallon, Charles Darbyshire, Thierry 
            Duprey Du Vorsent and Nick Houchin, Mohsin had completed this most 
            arduous of seafaring challenges in 76 days, 1 hour, 12 minutes and 
            42 seconds. As a team and as individuals this is a rare and 
            tremendous life-changing achievement. But the entire Oman Sail team 
            recognises the significance of the collective achievement lies in 
            Mohsin’s and Musandam’s specific success and what it means for Oman.<br>
            <br>
            <b>Taking on challenges</b><br>
            <img border="0" src="byinvitation2.jpg" align="right" hspace="8" vspace="6" width="300" height="202">While 
            Mohsin was on his journey round the world Oman’s national football 
            team won its first ever Gulf Cup. On 18 January 2009 a 6-5 win in 
            the penalty shoot-out sent a football-crazy and expectant nation 
            into a frenzy of joy. It had been a long climb, including the pain 
            of two final defeats in the previous two competitions losing to the 
            respective hosts in Qatar 2004 and UAE 2007. It was fitting that 
            Oman’s captain Mohamed Rabia should be the one to slot home the 
            winning penalty. Oman’s free-scoring midfielder Hassan Rabia had 
            been instrumental in the team’s success throughout the competition, 
            as had the parsimonious top-class goalkeeper Ali Al Habsy, who did 
            not concede a single goal in open play throughout the competition. 
            Ali had already brought great recognition to the nation as the first 
            Omani goalkeeper to play in the benchmark English Premier League as 
            a member of the Bolton Wanderers squad over the past 4 years.<br>
            <br>
            <img border="0" src="byinvitation4.jpg" align="left" hspace="8" vspace="6" width="250" height="305">But this was not the isolated success of individual stars who made 
            up the Oman team. Just like Mohsin and the crew of Musandam, Oman’s 
            footballing heroes had the support of all the backroom staff and 
            indeed the whole nation behind them, Omani and Expatriate alike. How 
            we cheered those moments of glory! Yet this was not simply the 
            euphoria of fleeting success, but a deepening sense that these very 
            public successes are a reflection of the lower profile but equally 
            important achievements of Omanis in all walks of life. A sense that 
            Oman’s remarkable Renaissance has reached a tipping point: That all 
            the effort of the last 39 years, standing on the shoulders of a rich 
            heritage and culture passed on over centuries by the nation’s 
            forefathers, is leading to the fact that Oman is a nation whose time 
            has come. <br>
            <br>
            <b>Role models</b><br>
            Such 
            a nation needs role models to epitomise its success and successful 
            adventurers and sporting heroes do just that. Sailing round the 
            world and winning major football tournaments are one thing; but look 
            around us at Omani doctors, nurses, teachers, cooks, lawyers, 
            carpenters, plumbers, welders, scientists, soldiers, policemen, 
            masons, cleaners, engineers, waiters, pilots, dentists, painters, 
            sculptors, photographers, travel agents, journalists, tour guides, 
            writers, poets, aid workers, supervisors, managers, electricians, 
            businessmen and many more. Every day young Omani men and women 
            graduate into employment or service in society at every level and in 
            all walks of life. This is their Everest. Omanis are being tested in 
            fire and they are not found wanting. Just one example: When we fly 
            safely in and out of Muscat International Airport the control tower 
            is manned exclusively by Omanis. This is the new watchtower of a 
            modern nation on the move.<br>
            <br>
            <img border="0" src="byinvitation3.jpg" align="right" hspace="8" vspace="6" width="300" height="199">This 
            is not to say that expatriates do not have a role to play. On the 
            contrary, expatriates are needed to take up jobs where there are, as 
            yet, an insufficient number of Omanis to meet demand. But the most 
            important role of the expat must be to pass the torch on to a new 
            generation of Omanis. The expat who understands that is worth his or 
            her weight in gold. There is little room for those who simply choose 
            to mock the faults and errors of the progress. We all stumble. We 
            all make mistakes. Not every industrialisation, real estate, or 
            economic diversification project will run smoothly. The market could 
            actually do with a lot more openness on failure and shortfalls. Not 
            everything is a runaway success. But what is undeniably successful 
            is the forward momentum of Oman’s economy, the leadership in 
            standards of its oil &amp; gas industry, and the individual and 
            collective successes of Oman’s people. So ignore the critics. It is 
            not the critic who counts. It is the man or woman who stands up to 
            be counted: The person who is willing to work hard and have a go. We 
            now read with great anticipation of another emerging role model with 
            Nabil Al Busaidy’s ongoing attempt to become the first Omani to walk 
            to the North Pole. ‘Attempt’ is the optimum word, because success in 
            any such endeavour is never guaranteed. The North Pole is a 
            position, not a place. It sits in a moving frozen sea and any one of 
            a number of factors can thwart his brave attempt. So we must see 
            heroism and inspiration in the very fact that he is willing to try 
            in the glare of public view.<br>
            <br>
            As Mohsin al Busaidi will tell you, no journey is without peril; or 
            as Ali Al Habsy and his team-mates will confirm, no victory in any 
            game or competition is guaranteed. If it was that easy, where is the 
            interest, and where the satisfaction? But there comes a moment when 
            time, effort, preparation and commitment pays off. 39 years into an 
            extraordinary Renaissance, Oman is a nation whose time has come.</font></p>
