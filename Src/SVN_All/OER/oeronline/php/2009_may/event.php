		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>“Winners Didn’t Do 
                Different Things, They Did Things Differently!”<br>
                </b></font>
                <font face="Verdana" size="1"><b>Shiv Khera’s one-day workshop 
                ‘Blueprint for Success – Turn Setbacks Into Comebacks’ elicits a 
                grand response</b><br>
                <br>
                <img border="0" src="event.jpg" align="left" hspace="8" vspace="6" width="300" height="213">Shiv Khera the internationally acclaimed author and management 
                guru recently held a self-improvement and personality 
                development interactive workshop in Muscat. The session covered 
                topics such as leadership, management, ethics and integrity 
                among others.<br>
                <br>
                The event that was brought to Oman by NPA Events. Held at the Al 
                Bustan Palace Hotel the day-long experience was supported by 
                Oman Mobile and Omantel. The event titled ‘Blueprint For Success 
                - Turn setbacks into comebacks’ - Inject New Vitality In Your 
                Organisational Culture Based on Ethics and Integrity – attracted 
                over two hundred senior level opinion and business leaders, top 
                and middle-level executives, and entrepreneurs from Oman’s 
                leading public and private sector enterprises and various 
                business houses.<br>
                <br>
                <b>Taking on challenges</b><br>
                The focus areas of this seminar was on how to manage and triumph 
                over these turbulent times, given the present economic slowdown 
                and the overall negative sentiments that is prevalent. Khera 
                deftly explained this through a series of analogies, real-life 
                examples, and stories to drive home his point that the “bigger 
                the problems, bigger the opportunities.”<br>
                <br>
                The motivational speaker also emphasised through many examples 
                how experience reveals throughout history that success has 
                resulted from overcoming problems. The more problems a person 
                solves the greater the reward he receives either in money or in 
                goodwill. Today, recession lends itself to one of the greatest 
                opportunities to those who are solution focused. Those who can 
                answer how it can be done rather than why it cannot be done.<br>
                <br>
                Also other interesting concepts on ‘performance’ and 
                ‘leadership’, the differences between ‘authority’ and 
                ‘influence’ and how to bring ‘personal values’ and ‘ethics’ at 
                work were discussed and debated. Group exercises that were 
                conducted helped in making the concepts easy to assimilate by 
                the participants.<br>
                Through this seminar, participants were also introduced to the 
                many concepts of how to get lasting results through positive 
                behavioural changes. By learning how to develop a culture that 
                creates trust not only the participant, but also his/her 
                colleagues and the organisation itself can benefit immensely 
                through them. Sections that would be of great use to the 
                organisation, through the participants, are about ‘empowering 
                people and making them accountable’.<br>
                <br>
                At a more personal level, ‘inspiring, influencing and getting 
                results’ and the knack of ‘building powerful teams that go the 
                extra mile’ would make this seminar not just a useful one, but 
                one that leaves the participants transformed for the better.<br>
                <br>
                Khera put to practical use his wide-ranging experiences in human 
                psychology and in boosting people’s sagging morale and 
                confidence. By analysing the issues and by showing the 
                participants the way to come up with workable solutions, that 
                can be drawn from within oneself, this one-day workshop provided 
                that much-needed relief to the participants. The thrust area of 
                Khera’s message was on focussing on the positive aspects more, 
                rather than dwelling on the negatives.<br>
                <br>
                Bringing the speaker at this time greatly helped in boosting the 
                spirits of the beleaguered business leaders of the Sultanate, 
                who could well use some professional help shared by someone of 
                Khera’s calibre and stature.<br>
                <br>
                <b>Everyone is a winner</b><br>
                Apart from the seminar itself, the participants of the workshop 
                were treated to many goodies – two BlackBerry Smartphones, 
                sponsored by the event presenters Omantel and Oman Mobile, a 
                gift hamper from Muscat Pharmacy, a grand buffet lunch arranged 
                at The Gulf of Oman Ballroom, at the Al Bustan Palace 
                InterContinental, and more usefully each participant received a 
                welcome pack that contained an autographed copy of Khera’s 
                international best seller ‘You Can Win’ which has sold over 
                one-and-half million copies worldwide, and is translated into 14 
                different languages. Khera was himself gifted a BlackBerry 
                Smartphone by the presenter of the event Oman Mobile and 
                Omantel’s senior manager, training and career development, Anwar 
                Masoud Al Kindi.<br>
                <br>
                After the successful completion of the event, Ashok Suvarna, 
                executive director, NPA Events, said, “We are delighted at the 
                way the day unfolded. It not only left the participants asking 
                for more, but it also provided them with various tools and 
                techniques that they could employ in bettering themselves both 
                personally and professionally.<br>
                <br>
                For the sponsors, it was a great opportunity to connect with the 
                right target group in a setting that was plush and ideal, to 
                share with them the features and highlights of their different 
                products and services. We are truly pleased to see how well this 
                event was received by the participants, and we have already got 
                many more such requests to conduct programmes of this nature in 
                the future”.<br>
                <br>
                The associate sponsors of the event were Khimji’s Watches; 
                Muscat Pharmacy; Oman Orix Leasing Company; New India Assurance 
                and the National Bank of Oman. The event’s media partners were 
                the Times of Oman; Alam Aliktsaad Wala’mal and Oman Economic 
                Review. The decor partner was Al Turki Enterprises and Ruwi 
                Modern Printers were the Print Partner. </font> 
