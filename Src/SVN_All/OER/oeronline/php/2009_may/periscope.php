            <font face="Verdana" size="1">
            <b>Mixed signals</b><br>
            The election in Malaysia are a clear vote of no confidence against 
            the new Prime Minister Najib Razzak even though he replaced a 
            quarter of the cabinet to highlight his commitment to economic 
            reform. Like Abdullah Badawi, the new Barisan coalition government 
            leader will face political gridlock and will be forced to accelerate 
            an economic stimulus. This means the Singapore dollar will 
            outperform the Malaysian ringett in the currency market.<br>
            <br>
            The South Korean won has strengthened to 1300 against the dollar for 
            the first time since January on funds inflows attracted by cheap 
            Korean shares and corporate debt. The 15 per cent rise in the South 
            Korean won demonstrates that financial markets no longer anticipate 
            an economic free fall. The South Korean won’s strength coincides 
            with the government’s three billion bond issue, the first sovereign 
            deal since 2006. However, I am reluctant to buy South Korean won 
            against the dollar as the easy money has been made and the risk 
            reward calculus no longer favours the dollar bears.</font></p>
