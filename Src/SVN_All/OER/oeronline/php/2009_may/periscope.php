            <b><font face="Verdana" size="4">Making 
            Money in Asian Currencies<br>
            </font>
            </b>
            <font face="Verdana" size="1"><b>
            Emerging markets were victims of the credit crunch, the ice age in 
            world trade and higher borrowing costs. as the Federal Reserve 
            shifted to a zero interest rate policy, emerging markets central 
            banks began to ease rates and sovereign debt credit risk spreads 
            compressed things may be changing for the better</b></font></p>
            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
            <font face="Verdana" size="1"><b>
			<font color="#CC3333">By Matein Khalid</font></b><br>
            <br>
            <br>
            <b>
            <img border="0" src="peri1.jpg" align="right" hspace="8" vspace="6" width="100" height="118"></b>The London G-20 has changed the rules of international finance. The 
            world’s heads of state have committed to fiscal stimulus, aggressive 
            monetary easing, reinvigorated IMF and a $250bn fund to boost trade 
            finance. The aggressive policy moves by the Fed, the Bank of England 
            and the European Central Bank (ECB) have reduced deflation risk and 
            put a safety net under the international banking system. This means 
            that panic buying of the Japanese yen as a safe haven no longer 
            makes sense.<br>
            <br>
            The Treasury-Eurodollar (TED) spread, a proxy for global banking 
            risk, has fallen from 450 basis points last October to only 95 now, 
            its pre-Lehman level. Global equity indices have surged one third 
            since early March. The Chicago Volatility Index (VIX) has fallen 
            from 80 to below 40. The Bank of Japan made it clear that it would 
            intervene in the currency market if the yen rose above 88 to the 
            dollar. The gnomes of Tokyo have therefore given the currency bazaar 
            a green light to sell the yen against the dollar, particularly since 
            the Tankan, a plunge in export orders and industrial production all 
            show the Japanese economy is in deep distress.<br>
            <br>
            The volatility in the Japanese yen option market has fallen to 15, 
            the lowest since Lehman. This is a compelling argument for carry 
            trades where investors borrow in the yen money market to buy risk 
            assets abroad. Japanese capital outflows also pressure the yen while 
            exporter demand for yen has plunged with the shrinking trade 
            surplus. The Japanese yen is headed for 104-106 against the dollar 
            in the next six months. <br>
            <br>
            <b>Financial support</b><br>
            Emerging markets were victims of the credit crunch, the ice age in 
            world trade and higher borrowing costs in the sovereign debt new 
            issue markets. However, as the Federal Reserve shifted to a zero 
            interest rate policy, emerging markets central banks began to ease 
            interest rates, sovereign debt credit risk spreads compressed after 
            the London G-20 pledge to treble IMF funding and the fall in 
            commodities.<br>
            <br>
            All of the above has profound implications on the behaviour of 
            emerging markets currencies. One, the G20 London conclave removes 
            the contagion risks that so gutted the asset class after the 
            Russia/Asia crises of 1998. Two, external financing gaps in most 
            markets, apart from the Baltics/Ukraine, are not systemic. Three, 
            the cross-border plunge in global capital markets took place in late 
            1998 as hedge funds unwound their offshore, leveraged bets. This was 
            the reason that correlations between emerging markets FX, debt and 
            equities tripled to 0.9, a telltale sign of distress selling.<br>
            <br>
            The Turkish lira is an extremely attractive emerging market 
            opportunity. One, Recap Tayip Erdogan’s government won the recent 
            municipal elections and reinforced its parliamentary majority in 
            Ankara.<br>
            <br>
            Two, the Justice and Development Party (AKP) will accept a $40bn IMF 
            letter of credit in exchange for fiscal discipline. This is a huge 
            boost for investor confidence in Turkey as it eliminates refinancing 
            risk.<br>
            <br>
            Three, Turkish real interest rates remain among the highest in EMEA 
            even though the central bank has slashed nominal rates by more than 
            600 basis points.<br>
            <br>
            Four, Obama’s visit demonstrates the strategic value of Turkey in 
            the Middle East if the US exits from Iraq or mediates in the 
            Palestine peace process.<br>
            <br>
            Five, Turkish shares are extremely cheap at only seven times 
            earnings with Istanbul a natural magnet for fund managers 
            disillusioned with Russia and Central Europe. This suggests that the 
            Turkey Fund (symbol TKF on the New York Stock Exchange) could rise 
            from five to seven in the next six months as the lira surges from 
            1.60 now to 1.44-1.48. This is a cheap, undervalued emerging market.<br>
            <br>
            <b>Factoring in a victory </b><br>
            It is evident that the financial markets have priced in a Congress 
            victory in the Indian elections, with a thirty per cent rise in the 
            Sensex in the past month and the Indian rupee below 50 against the 
            dollar. Yet the Sensex rally is living on borrowed time as profits 
            will plunge in 2009 across sectors and the election outcome can well 
            lead to a weak, fragmented parliamentary coalition.<br>
            <br>
            While a Congress win will lead to a short term snapback to 48 on the 
            dollar-Indian rupee FX rates, the bearish case for the INR will 
            resume and the Indian Rupee will fall to 54-56 against the dollar by 
            the end of 2009. Indian GDP growth cannot accelerate beyond 4.5 per 
            cent amid a global recession, a squeeze in domestic borrowing costs, 
            a crowded out private sector and an imminent property crash. 
            Congress’s populist, pre-election fiscal profligacy has also 
            bequeathed India with the all too real possibility of a downgrade of 
            Indian sovereign debt below the BBB minus assigned by S&amp;P.<br>
            <br>
            It was reckless, if politically expedient, for the government to 
            increase the pay of government civil servants, waive farmers’ loans, 
            slash excise duties and services/VAT amid a global financial crisis, 
            which makes a one digit government fiscal deficit inevitable. As 
            remittances from the Indian diaspora plunge, India’s current account 
            deficit (ex remittances) could rise to four per cent. With modest 
            global cross-border capital flows and high risk of a sovereign debt 
            downgrade, the Indian rupee could well depreciate to 54 against the 
            dollar by end 2009.<br>
            <br>
            Indonesia under President Yadhoyono is one of the clear economic 
            success stories of Southeast Asia and the incumbent government will 
            probably win the imminent July election. The President’s Democratic 
            Party tripled its vote in legislative election and will easily lead 
            a credible governing coalition. Indonesia, though slammed by the 
            plunge in commodities markets and Wall Street risk aversion, is 
            primarily a domestic economy, with exports a mere 10 per cent of 
            GDP.<br>
            <br>
            However, Indonesia’s financial Achilles heel is a $150bn external 
            debt, thrice the hard currency reserves of the Jakarta central bank. 
            The Indonesian central bank lost $12bn trying to defend the rupiah 
            in the immediate aftermath of the Lehman Brothers crisis and was 
            even forced to arrange swap lines with the US, China, Japan and 
            South Korea. As Hillary Clinton’s visit to Jakarta suggests, the 
            Yudhoyono government is allied to Washington and has attracted $6bn 
            from the World Bank, the ADB and even the Islamic Development Bank. 
            The parliamentary poll results are bullish for the President’s 
            reelection, reforms, FDI, bank support and sovereign risk appetite. 
            The Indonesian rupiah could well appreciate to 10,000 in the next 
            six months.</font></p>
