            <img border="0" src="cover13.jpg" align="left" hspace="8" vspace="6" width="250" height="325">AL Hassan Engineering Co (AHEC) is a company that has had a stellar 
            track record in executing projects across industries. In fact, in 
            the field of power generation, the company has installed 
            approximately 40 per cent of the generating capacity of Oman. AHEC 
            provides electrical, mechanical, instrumentation and civil 
            contracting services to an array of industries. Over the years, the 
            company has performed well, completing a number of major projects. 
            “In 2008, the company saw its turnover increasing by 28 per cent in 
            comparison to 2007 and strong and significant order intake. In fact, 
            the order intake will take us through 2009,” says Peter Hall, CEO of 
            the company. <br>
            <b> <br>
            Healthy order book </b> <br>
            In the oil, gas and petrochemicals sector, the company was awarded 
            at the beginning of 2008, an EPC contract from Petroleum Development 
            Oman (PDO) for their Full Field Water Injection project at Nimr. 
            AHEC is on schedule for the project which is approaching 50 per cent 
            completion and it has successfully completed one year without a 
            ‘Lost Time Injury Incident.’ For Korean company, GS Engineering, 
            AHEC is in the closing stages of completing two major civil, 
            mechanical and pipework contracts, one on the Sohar Aromatics plant 
            and the second at Salalah Methanol plant.<br>
            <br>
            In the early part of the year, AHEC secured a multi-million dollar 
            contract for the Burhan West and Harmal Pipeline project from the 
            government gas department. The project which will be managed by PDO, 
            further strengthens AHEC’s reputation as one of the few contractors 
            in Oman having capabilities to handle exotic materials such as 
            duplex stainless steel (DSS). As far as the power sector is 
            concerned, AHEC has an extraordinary record in the field, having 
            installed approximately 40 per cent of the generating capacity of 
            Oman. AHEC is currently in the closing stages of completing what 
            will be the largest power plant in Oman. Working with the French 
            manufacturer Alstom, the 1,000 MW gas turbine combined cycle power 
            plant for Sohar Aluminium will be fully commissioned in the coming 
            months. During 2008, AHEC completed work on the installation of the 
            largest power plants on the PDO system at Mukhaizna (250 MW) &amp; Qarn 
            Alam (125 MW). In addition, AHEC is currently executing the job of 
            once-through heat recovery steam generators (HRSG) contract for Gulf 
            Steam Generators – a subsidiary of NeM Netherlands and a leader in 
            the field of steam generation. Another project involving steam 
            generating equipment is the cogeneration plant being executed for 
            Hirbodan-HICO at ORPC in Mina Al Fahal, which includes two heat 
            recovery steam generators and one auxilliary boiler.<br>
            <br>
            <b>Tapping the regional market<br>
            </b>In 2009, AHEC made its entry into the regional market, securing an 
            order from Siemens to undertake civil works associated with a new 
            Dubai Electricity and Water Authority’s substation in Dubai. In the 
            water sector, AHEC secured the civil work contract from Korean 
            company Hyundai Rotem for the Seeb Wastewater treatment plant which 
            will be completed for Haya Water over the next two years. AHEC has 
            implemented several initiatives in the areas of communications, IT, 
            quality, procurement, human resources and corporate social 
            responsibility in 2008. “The company went in for a major revamp of 
            its website keeping in mind its global audience of potential 
            partners, job seekers, customers, journalists and investors. Google 
            analytics reports have shown a 150 per cent increase in the number 
            of unique website visitors,” says Hall.<br>
            <br>
            AHEC is also undertaking other systemic improvements. It has 
            expanded the existing Oracle based ERP to include areas and 
            departments that have hitherto not been covered. It has seen 
            significant savings in the areas where this system is currently 
            operational. In order to further streamline its procurement 
            function, AHEC moved part of its procurement processes online via 
            ‘Tejari Transact’, a Business-to-Business (B2B) electronic 
            marketplace. <br>
            <br>
            Omanisation has always been a strategic priority for AHEC. 
            Continuing the drive of inducting quality Omani manpower, qualified 
            engineers were recruited in different disciplines of the business. 
            AHEC’s key corporate social responsibility initiatives in 2008 
            include sponsoring International Conference on Harnessing Technology 
            and BG Energy Challenge.</font></p>
