            <td width="519" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><br>
                <b><font face="Verdana" size="4">Asian flavour</font></b><font face="Verdana" size="1"><strong><br>
            Malaysia has pulled out all the plugs to attract attention to itself 
            not only through its tourism but also by offering the best in 
            education and banking, writes Visvas Paul D Karra after a 
            free-flowing animated chat with HE Dato Mohd Zamri Mohd Kassim, the 
            Malaysian Ambassador</strong><strong style="font-weight: 400"><br>
            <br>
            <img border="0" src="face.jpg" align="left" hspace="8" vspace="6" width="300" height="398">If there is one country, which has become famous as a tourist 
            destination, then it has to be Malaysia. The slogan “Malaysia, truly 
            Asia” has catapulted Malaysia from being just another south-east 
            Asian nation to ‘the’ country which every tourist worth his salt 
            would have on the top of his most-visited-places list.<br>
            <br>
            The march of Malaysia from a British colony to its present 
            ‘Malaysia, truly Asia’ stature has been dramatic and worth 
            emulating. And if there is one person who has played the major role 
            in effecting this change, then it is Dr Mohammed Mahathir, who 
            guided the country through the late 80s, 90s and into the 21st 
            century. No discussion about Malaysia is complete without the 
            mention of this charismatic personality.<br>
            <br>
            </strong><strong>
            Mahathir momentum</strong><strong style="font-weight: 400"><br>
            “Dr Mahathir’s visionary and revolutionary thinking has helped 
            Malaysia to transcend the traditional setbacks and change the 
            landscape of the country by eliminating government inefficiency and 
            encouraging the private sector in nation building,” says HE Dato 
            Mohd Zamri Mohd Kassim, Malaysian Ambassador.<br>
            <br>
            “In any country there is resentment against transformation. In order 
            to overcome this, Dr Mahathir began the ‘leadership by example’ 
            campaign. He began the “Islamisation” of the civil service to 
            inculcate honesty and sincerity among government workers and 
            introduced the punch card system to regulate their working hours. He 
            would conduct spot inspections to assess the efficiency of workers. 
            Now the results are there for everyone to see,” the ambassador said 
            while talking about the fourth prime minister of Malaysia since it 
            became an independent nation in 1953.<br>
            <br>
            Today, Malaysia is the epitome of a modern Islamic nation. It is the 
            global hub of Islamic finance; the F1 calendar has the Malaysian 
            Grand Prix; the country’s tourism attractions have become the byword 
            for leisure activities; Proton car is manufactured here; and it has 
            the world’s tallest tower – the Petronas Tower – a symbol of the 
            country’s all round prowess.<br>
            <br>
            The articulate HE Kassim says, Malaysian leaders have always 
            believed in people development and therefore today more than 60 per 
            cent of the national budget is spent on education. Malaysia has some 
            of the world’s finest education institutes, says the ambassador 
            while talking of Malaysian social development. More than 200,000 
            students pass out annually from a number of colleges and 
            universities thus generating a huge talent pool penetrating all 
            sectors of the economy and giving Malaysia a cutting edge over the 
            rest of the south east Asian nations.</strong></font></p>
            <p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
            <img border="0" src="face1.jpg" width="414" height="264"></p>
            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1"><strong style="font-weight: 400">The Malaysia Centre of Excellence and the Malaysia Technical 
            Education Institute continue to draw foreign students and Omanis are 
            also enrolling themselves in these two facilities. The Malaysian 
            government has plans to attract more number of students by offering 
            scholarships and incentives. Prosper thy neighbour and thou shalt 
            prosper. This is the premise on which the Malaysian government 
            operates. Therefore, a number of foreign students get sponsorships 
            and scholarships to come and study in Malaysia.<br>
            <br>
            Proton car is already on the roads of Oman and some energy companies 
            are already eyeing the Omani market, says Dato Mohammed, while 
            speaking about the bilateral trade between Oman and Malaysia. Apart 
            from Proton and Petronas, some of the world’s best think tanks are 
            located in Malaysia, says the ambassador while disclosing that 
            Malaysian expertise in software and hardware is also unmatched. The 
            world’s smallest microchip is also produced in Malaysia.<br>
            <br>
            </strong><strong>
            Correcting the imbalance</strong><strong style="font-weight: 400"><br>
            Presently, the balance of trade (see box) is in favour of Oman 
            mainly due to the bulk import of oil from Oman, which constitutes 
            about 90 per cent of the total trade volume while the other notable 
            items include agriculture products. Exports from Malaysia to Oman 
            include palm oil, machinery and electronic items like televisions, 
            and automotive parts. Apart from the Proton cars, which are already 
            plying on the Omani roads, other exports from Malaysia to the 
            Sultanate include technology transfer in oil and gas; oats; herbal 
            drinks; and construction equipment.<br>
            <br>
            <img border="0" src="face2.jpg" align="right" hspace="8" vspace="6" width="300" height="256">The Malaysian ambassador said that he is trying to normalise the 
            trade imbalance by widening the scope of collaboration between the 
            Sultanate and Malaysia. “I am going across the board and trying to 
            push all the ministries and the private sector companies to come 
            here to Muscat and explore the opportunities in this beautiful 
            country. Malaysian businessmen must come and offer their expertise 
            in construction of roads, IT solutions, real estate and mining,” HE 
            Kassim said.<br>
            <br>
            The ambassador is aware of the massive development path, which Oman 
            has embraced and the resultant business and investment opportunities 
            that have been created by this Renaissance under the wise leadership 
            of His Majesty Sultan Qaboos bin Said. “I don’t want Malaysia to 
            miss the opportunity boat which the Sultanate of Oman has set 
            sailing. But we would like to start small, with one or two 
            companies. If one company spreads the word about Oman then it will 
            continue to spread,” he said.<br>
            <br>
            </strong><strong>
            Bilateral activities</strong><strong style="font-weight: 400"><br>
            The Oman-Malaysia Business Forum, which was held in 2008, has been a 
            revelation to both sides. HE Ahmed bin Abdulnabi Macki, Minister of 
            National Economy and Deputy Chairman of the Financial Affairs and 
            Energy Resources Council; and HE Khalil Al Khonji, Chairman of OCCI 
            attended the forum. Their Malaysian counterparts, headed by YB Dato 
            Seri Rafidah Aziz, MITI minister, represented a plethora of economic 
            interests and included the State governors of Malacca and Perlis; 
            and a number of parliamentarians. An interactive session was held 
            between 227 Omani private sector companies and 197 Malay 
            counterparts. As a followup to the 2008 business forum, a flurry of 
            Malaysian visits also ensued.<br>
            <br>
            Oman and Malaysia are expected to sign memorandums of understandings 
            for avoidance of double taxation and institutionalisation of a legal 
            framework on investment guarantee agreement on trade by the end of 
            this month. A number of MoUs on education, civil aviation (signed in 
            1993), information (1998), social development and women, private 
            investments are already in place.<br>
            <br>
            Speaking about other bilateral activities that are happening between 
            the two countries, HE Kassim said that more than 19,000 Omani 
            tourists visited Malaysia from January-November, 2008. The embassy 
            is working with its Dubai-based tourism office to encourage more 
            Omanis to visit Malaysia. The Malaysian government has also 
            increased its tourist spend, disclosed the ambassador while saying 
            that aggressive promotion of their country is being done through the 
            media.<br>
            <br>
            Malaysia is also promoting medical tourism in a big way and once 
            direct air links between Muscat and Kuala Lumpur are established, 
            travel activities between the two countries will get enhanced 
            leading to an increase in visitor numbers.<br>
            <br>
            Malaysia’s Labwa region is also fast turning into an offshore 
            financial centre with all established banks setting up their 
            branches there.<br>
            <br>
            People of every race, culture and creed can feel immediately at home 
            in Malaysia because of its mixed demographic profile. Besides the 
            ethnic Malays, the country has a large number of Indians, Chinese 
            and other nationalities who all make Malaysia, the melting pot of 
            cultures.</strong></font></p>
