            Green Awards 2009 – together we can</font></b><font face="Verdana" size="1"><br>
            <br>
            <img border="0" src="news1.jpg" align="right" hspace="8" vspace="6" width="170" height="147">“We must be the change we wish to see in this world. There is enough 
            for everyone’s need but not for everyone’s greed.” – <b>Mahatma Gandhi</b><br>
            <br>
            Global warming, acid rain, ozone depletion, coral life degradation, 
            carbon emissions, draughts, wildlife fires… the catastrophic 
            consequences of human apathy and neglect towards the environment can 
            go on and on. As the world wakes up to the follies of mankind, Going 
            Green has become the global metaphor to save the world from the 
            deleterious effects of industrialisation and modernisation. Economic 
            experts have suggested that creating a green economy is going to top 
            the agenda of most corporates by the turn of this decade as every 
            company worth its salt strives to contribute its mite to protect and 
            restore what is left of the Earth and our environment. <br>
            <br>
            <b>Breaking new ground</b><br>
            Oman Economic Review Green Awards (OERGA) 2009 is an effort to raise 
            consciousness about environment protection and sustainable 
            development in the Sultanate. As the country’s first Environmental 
            Awards, OERGA is a call to action for behavioural change to save 
            planet earth for the generations to come. <br>
            <br>
            Born out of the unwavering belief that our Earth is a shared 
            resource and therefore a shared responsibility, these awards will 
            recognise and promote voluntary contributions across a broad 
            spectrum of activities both from individuals and companies. Says 
            Sandeep Sehgal, CEO, United Media Services, “OERGA will raise 
            awareness about the green issues facing the Sultanate and will 
            provide a good opportunity to showcase the positive commitments of 
            Oman’s corporate world to this country and its people, culture and 
            environment. It will also highlight the moral responsibility being 
            shared by every company and individual towards this precious planet 
            that we call earth.” Businesses – large and small; institutions – 
            educational, health, or governmental; and individuals – 
            independently or as leaders of a collective whole, all play a 
            crucial role in environmental protection and preservation. OER Green 
            Awards 2009 is a platform to honour and appreciate such outstanding 
            environmental vision, endeavours, initiatives and achievements.<br>
            <br>
            While the misdeeds of the past cannot be undone, we can certainly 
            join hands to save our future. For further information visit
            <a href="http://www.oeronline.com/greenawards">
            www.oeronline.com/greenawards</a></font></p>

