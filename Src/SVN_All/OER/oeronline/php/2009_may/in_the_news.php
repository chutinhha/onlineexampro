            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
            <font face="Verdana" size="1">
            The Culinary Guild of Oman held its second ever meeting at the 
            Crowne Plaza hotel on April 18. Present at the meeting were some of 
            the best chefs in Oman from various leading companies and 
            restaurants, including Al Bustan Palace Hotel, Intercontinental 
            Muscat, Oman Air and more. The primary objective of the conference 
            was to set the foundation for the first ever Chefs Guild in Oman. 
            United Media Services is a strategic partner of Oman’s Chef Society. 
            One of the key points discussed was the way chefs were viewed by 
            society at large. The consensus was that chefs were seen as mere 
            cooks and it was imperative that they got the recognition they 
            deserved. It was felt that this would go a long way to help smaller 
            restaurants gain respectability. Another key point was to involve 
            the guild strongly with various colleges and institutions and to 
            assist them in bridging the gap between a professional chef’s 
            expectations and the college curriculum.</font></p>

