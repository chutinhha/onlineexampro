                <br>
                </font><b><font face="Verdana" size="4">Time for a rethink<br>
            </font></b>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b>Time 
            magazine listed him as one of India’s most influential people in 
            2008 stating, ‘Ahluwalia’s fingerprints can be found in policies 
            from economics to education reform, and he has been a driving force 
            in the liberalisation programme of the past 16 years.’ Montek Singh 
            Ahluwalia, Deputy Chairman, Planning Commission, India was on an 
            official visit to the Sultanate to discuss bilateral economic 
            relations recently. He takes time out from his busy schedule to talk 
            to Mayank Singh<br>
            </b><br>
            <b>
            <img border="0" src="60min.jpg" width="300" height="318" align="left" hspace="8" vspace="6">Is the global financial meltdown a crisis of confidence more than 
            anything else?</b><br>
            I would say that it’s a genuine financial muck up, which is 
            compounded by a crisis of confidence, resulting from the fact that 
            people are not very clear whether a solution is being found. I am 
            persuaded that the authorities in the US are aware of the 
            seriousness of the crisis and the need to take strong action. You 
            have seen a financial crisis which is a reflection of the failure of 
            a certain approach to financial regulation that till now has been 
            well recognised. I think people know what went wrong and they are 
            determined to set it right. Governments are prepared to recapitalise 
            the banks, the uncertainty lies in whether the methods being found 
            will be politically acceptable and once people see that all has 
            fallen into place, then will they be able to overcome the pure 
            confidence problem. <br>
            <br>
            <b>Globally, an unprecedented amount of work has gone into tackling 
            this crisis. Is <br>
            that working?</b><br>
            Well, there are different things that require global action. There 
            are four areas globally which are important – action to fix the 
            financial system right now, you cannot do it globally, but each 
            country has to do it individually. I am persuaded that the US is 
            aware of what the scale of the problem is and the lead that they 
            have to give domestically and they said it so categorically at the 
            London G-20 meet, so I assume that they are going to do it. I think 
            once the Americans fix the problem, then nobody will tolerate the 
            Europeans not fixing their problem, so they will follow suit. Point 
            number two, is are we putting in place a regulatory structure for 
            the future. The answer is yes, we are as best as we can, but in 
            future there will be smarter people who will avoid these regulations 
            better than the people who maintain them and this is not the 
            important thing. This is important but not important urgently. As it 
            is, I do not think that any bank will do anything risky for the next 
            two years. So even if you did nothing it is going to be difficult to 
            persuade banks to lend. The discussion on implementing financial 
            regulations is going on well and the convergence that they have got 
            on ideas is good, the global financial framework is getting 
            democratised -- the Financial Stability Forum and the Basel 
            Committee on Banking Supervision have been expanded to include the BRIC countries. The next thing is money for the financial 
            institutions to help developing countries and there the performance 
            of the G-20 summit has been very good. They have never done this 
            much before, some may argue that there has never been such a big 
            crisis before. If any individual country had fallen into a crisis 
            there would have been homilies about how their individual systems 
            are at fault. Overall, there is a recognition that this problem was 
            caused by the industrialised countries so there is a moral 
            willingness to do something more than the minimum and in term of 
            numbers it is pretty much the maximum. In Davos, I was on a panel 
            with John Lipskey, the first deputy managing director of IMF, and he 
            said that the funding sources (for developing countries) should be 
            doubled by another $250bn and I said that it should be trebled to 
            $500bn and that’s what they have done, so how can we complain. The 
            last thing is protectionism. It is very tough and no single 
            communiqué can solve the problem but they have put in place a 
            monitoring system and these are steps in the right direction. In a 
            global crisis of this magnitude, you cannot think of a global 
            conference as a pre-surgery to solve the problem, it is more a 
            meeting of minds. Most people think that the recovery that the IMF 
            has postulated for 2010, requires the same order of stimulus in 2010 
            as 2009. But at this moment that is not on, as they are going to 
            withdraw stimulus on the grounds that they have done enough. But the 
            agreement is that governments will watch the situation and if by 
            September 2009 there is a feeling that things are bottoming out then 
            there is no need for a stimulus, but if things do not improve then 
            there is a chance they will be willing to do more.
            </font>
              </p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="60min1.jpg" hspace="8" vspace="6" width="450" height="251"></font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>When the G-20 Summit began people went in with few expectations, 
            but by the time it ended there seems to be a meeting of minds of 
            sorts?</b><br>
            When people went in, nobody had major expectations, Sarkozy was 
            quoted as saying that if I do not get a global regulator, I will 
            walk out. Whether newspapers are building up these stories or 
            whether he said these things I am not sure, but when leaders reach 
            there, then they sense a certain mood and reposition themselves. No 
            leader wanted to be isolated, even those who wanted to do. Nothing 
            substantive has been done to reform the Bretton Woods institutions. 
            The fact that the leaders of these institutions are going to be 
            chosen from an open process is an important step forward. There is 
            also a rethink of their mandate seeing what kind of a role they will 
            be performing, Gordon Brown, the chairman of G-20 has been 
            authorised to speak to people and come back. The summit has provided 
            money to the system as it stands, without any real reform of the 
            system, but they recognise that a major global crisis is going on 
            and the first priority is to tackle the crisis. Once you tackle the 
            crisis then you get a moral credibility to do reforms and the 
            reforms should be done stating that what made it more difficult to 
            tackle the crisis and what was it that actually triggered the 
            crisis. Everybody has shown flexibility – industrialised countries 
            by providing money, developing countries by holding back a demand 
            for immediate reform, everybody by talking about protectionism in a 
            way that it sounds right. A very important thing is that, in the 
            past, crisis affecting the developed world never involved the 
            developing world around the table, how effective our presence was, 
            only history will tell but the very fact that we are sitting there 
            and talking about fiscal crisis and getting into the discussion on 
            the global side reflects a huge mindset change. In the old days if 
            there was a global problem of this kind then the G-7 would have had 
            a discussion.<br>
            <br>
            <img border="0" src="cover104.jpg" align="right" hspace="8" vspace="6" width="188" height="273">The western view on financial systems was that developing countries 
            should completely integrate their financial systems with the rest of 
            the world. And as foreign banks were more efficient than your 
            (developing countries) banks, you did not have to build your own 
            financial system and just let the Citibanks and Chase Manhattans to 
            take care of that. We (in India) never accepted this view – we 
            wanted to build our own financial systems and let foreign banks be 
            there to compete and that was the right strategy, if we had 
            completely relied on foreign banks then we would have had a massive 
            credit squeeze right now. Even today,ß foreign banks are actually 
            contracting credit to preserve the risk to their capital. Our banks 
            are also looking at risk aversion, but that is something that we can 
            take care of. <br>
            <br>
            <b>What led to the global crisis in your opinion?<br>
            </b>The basic Anglo Saxon economic model was light regulation and 
            relying on market discipline and enlightened risk management and 
            this would provide you with all the incentives for innovation 
            without running any risks. This model has proved to be 
            comprehensively wrong. The crisis has shown that the financial 
            markets are different from actual markets and they need stricter 
            regulation. There was a lot of regulatory arbitrage with some guys 
            being subject to regulation while the others were not subject to 
            regulation, wings of the same institution were regulated here and 
            not regulated there. The originate and distribute model, which was 
            supposed to distribute risks to those who were better able to manage 
            it turned out to be only disguising risk and reconcentrating them in 
            the banks themselves. They were meant to go out of banks but they 
            got concentrated in other vehicles which involved the reputational 
            and balance sheet risk of the bank so it was a huge mistake. The new 
            regulatory structure that they are thinking about will take care of 
            that. The leverage in the system was far too high and in future it 
            will be less. Whether that will hurt developing countries is an open 
            question. In one way you could argue that by creating these 
            complicated structures, these guys were fooling the world by stating 
            that you could achieve good returns and low risk returns by 
            investing in the US. Without these structures it would be very clear 
            that the returns in the US are low. The other side of the coin is 
            that if you want returns then go for FDI and things like that in the 
            developing world, of course there is a little bit of more risk 
            there, but what was happening here was that they were saying that we 
            can give you the returns without the risk, which was not the case. 
            So I do not think in net terms developing countries which are 
            capable of growth will lose out. If India can project itself as a 
            safe and attractive destination then it will benefit from future 
            capital flows. <br>
