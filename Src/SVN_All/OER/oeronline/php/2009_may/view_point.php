		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>A 
                business haven</b></font><font face="Verdana" size="1"><br>
                <b>A diversified free-market economy with a developed tourism, 
                manufacturing and financial services industry Malta offers 
                international investors a highly advanced telecommunications 
                network, skilled professionals and a strategic location<br>
                <br>
                <font color="#CC3300">By Sridhar Sridharan</font><br>
                </b><br>
                <br>
                    <img border="0" src="view.jpg" align="right" hspace="8" vspace="6" width="100" height="119"><b>Business environment</b><br>
                The Maltese Archipelago, made up of Malta, Gozo and Comino, are 
                located 93 kilometers off the southern coast of Sicily and 290 
                kilometers north of the shores of North Africa.<br>
                <br>
                Malta joined the EU in 2004, and subsequently adopted the euro 
                on January 1, 2008. The euro adoption has made Malta more 
                attractive for investment purposes through reductions in 
                transaction costs and international trade barriers. Given 
                Malta’s membership in the EU, its legislation is reflective of 
                EU legislation and directives, providing foreign investors in 
                Malta with the assurance of the quality and consistency 
                synonymous with the EU. Over the last few years, Malta has 
                adopted and established a comprehensive legislative and 
                regulatory framework for financial service activities based on 
                standards of best practice.<br>
                <br>
                FinanceMalta, a foundation formed between the government and the 
                industry, was founded with the aim of further improving Malta’s 
                profile as a finance centre on a worldwide basis. FinanceMalta 
                strives to maintain a modern and effective legal, regulatory and 
                fiscal framework.<br>
                <br>
                <b>Economic overview </b><br>
                <img border="0" src="cover103.jpg" align="left" hspace="8" vspace="6" width="300" height="172">Malta has a diversified free-market economy which mainly relies 
                on tourism, manufacturing and financial services. The government 
                encourages foreign investment and Malta enjoys good industrial 
                relations. Malta also provides numerous financial incentives and 
                maintains a low tax regime encouraging economic growth. The 
                island offers international investors a highly advanced 
                telecommunications network, skilled professionals and a 
                strategic location. Malta has a number of key features which 
                make it attractive for foreign investors, including:<br>
                <br>
                a. A stable macroeconomic environment, and taxation and 
                financial infrastructure <br>
                b. A broad and advantageous double taxation treaty network <br>
                c. A fast and approachable regulatory authority <br>
                d. Central time zone which facilitates global coverage <br>
                <br>
                <b>Incentives</b><br>
                The Malta Enterprise Act has introduced a new set of incentives 
                for the promotion and expansion of business, covering a wide 
                range of sectors and activities. The incentives available under 
                the Act include access to finance, investment aid, SME 
                development, research and development programmes, enterprise 
                support, and employment and training.<br>
                <br>
                </font>
                <b><font face="Verdana" size="2">Structure of business entities </font><font face="Verdana" size="1"> <br>
                </font>
                </b><font face="Verdana" size="1"><br>
                <b>1. Corporate forms <br>
                </b><br>
                The most common form of business entity in Malta is the limited 
                liability company. These may be either public or private 
                companies, and are governed by the Companies Act. Upon 
                submission of the Memorandum and Articles, and if all legal 
                requirements are satisfied, a company is registered. <br>
                <br>
                A private company has restrictions on the extent to which it can 
                transfer its shares. It prohibits any invitation to the public 
                to subscribe to its shares, and the number of shareholders 
                cannot exceed 50. A public company may offer shares or 
                debentures to the public. A Partnership En Nom Collectif has its 
                obligations guaranteed by the unlimited and joint and several 
                liability of all the partners. A Partnership En Commandite has 
                its obligations guaranteed by the unlimited and joint and 
                several liability of its general partners, and by the liability, 
                limited to the amount of their contribution, of one or more 
                limited partners.<br>
                <br>
                <b>2. Partnerships and Sole Traders <br>
                </b><br>
                Sole proprietorship and partnership are generally subject to 
                lesser statutory requirements than companies, but the partners 
                and the proprietors do not have the protection of limited 
                liability.<br>
                <br>
                <b>3. Joint ventures <br>
                </b><br>
                Joint ventures are usually formed using the company structures 
                described above.<br>
                <br>
                <b>4. Branches of foreign companies <br>
                </b><br>
                Overseas companies establishing a place of business in Malta 
                should, within one month of the establishment of the place of 
                business, deliver to the Registrar an authentic copy of the 
                charter of the company, a list of the officers of the company, 
                and the names and addresses of one or more individuals resident 
                in Malta authorised to represent the overseas company in Malta.
                <br>
                <br>
                <b>
                <img border="0" src="cover102.jpg" align="right" hspace="8" vspace="6" width="200" height="512">Structures used by foreign investors <br>
                </b>A Collective Investment Scheme is an entity licensed under 
                the Investment Services Act. Gains or profits derived by 
                Collective Investment Schemes are exempt from tax (except for 
                income derived from immovable property situated in Malta and 
                investment income from Maltese sources).<br>
                <br>
                The most common corporate structure of a Maltese fund is the 
                company with variable share capital (SICAV - open-ended fund). 
                The Companies Act also provides for the setting up of a company 
                with fixed share capital (INVCO - close-ended fund). <br>
                <br>
                There are also hedge funds which are usually in the form of 
                Professional Investment Funds (PIF) which are ultimately forms 
                of Collective Investment Schemes and are incorporated as a SICAV 
                or INVCO, or as a limited partnership, mutual fund or a unit 
                trust. <br>
                <br>
                <b>Tax implications applicable to forms of business structures 
                </b><br>
                Companies resident in Malta are subject to income tax on their 
                worldwide income. Companies registered outside Malta are 
                considered residents of Malta if management and control is 
                exercised in Malta. Companies that are neither resident nor 
                domiciled in Malta are subject to income tax only on their Malta 
                source income and Malta source capital gains. Foreign source 
                capital gains derived by persons who are not domiciled or 
                ordinarily resident are not taxable in Malta at all. <br>
                <br>
                Income tax is the only tax imposed on the profits of companies. 
                The standard rate of income tax is 35 per cent.<br>
                <br>
                Tax losses incurred in a trade or business may be carried 
                forward indefinitely to offset future income. Unabsorbed capital 
                allowances may also be carried forward indefinitely, but may 
                only offset income derived from the main source. A carry back of 
                losses or capital allowances is not allowed.<br>
                <br>
                <b>The refundable tax credit system </b><br>
                In 2007, a refundable tax system for all companies distributing 
                dividends to shareholders was introduced. The tax paid by a 
                company is essentially treated as a prepayment of tax on behalf 
                of the shareholder. The new refundable tax system applies both 
                to profits allocated to a company’s Maltese Taxed Account and to 
                profits allocated to its Foreign Income Account and is available 
                both to residents and non-residents.<br>
                <br>
                The refundable tax system is extended to shareholders of foreign 
                companies that have Maltese branches. Tax paid in Malta by 
                branches on profits attributable to activities performed in 
                Malta is refunded when such profits are distributed. Persons 
                must register with the Commissioner of Inland Revenue to benefit 
                from the tax refunds described above. <br>
                <br>
                <b>Relevant tax exemptions </b><br>
                The Income Tax Act exempts from tax interest, discount, premium 
                or royalties accruing to or derived by non-residents. <br>
                <br>
                <b>Withholding taxes </b><br>
                A final withholding tax of 15 per cent is imposed on dividends 
                paid out of untaxed income to Maltese resident individuals and 
                bodies of persons, other than companies. Dividends, interests 
                and royalties paid to non-residents are not subject to 
                withholding tax regardless of whether they are paid out of taxed 
                or untaxed.<br>
                <br>
                The Parent Subsidiary Directive applies to parent companies and 
                their subsidiaries which are incorporated in member states of 
                the European Union. This directive ensures fiscal neutrality 
                between the parent company and its subsidiary by exempting from 
                withholding tax the profits which a subsidiary distributed to 
                its parent company.<br>
                <br>
                <b>Individual income tax </b><br>
                Different progressive tax rates apply to married couples 
                resident in Malta, individuals’ resident in Malta and 
                non-resident individuals with a maximum marginal rate of 35 per 
                cent. <br>
                <br>
                <b>Other taxes</b><br>
                Value Added Tax (VAT) is the main indirect tax in the Maltese 
                tax system. The other significant taxes include duty on 
                documents and transfers and property transfers tax. <br>
                <br>
                <b>Double tax relief</b><br>
                Malta has signed a number of Double Tax Treaties. Malta has 
                signed a Double Tax Agreement with Oman but the agreement is not 
                yet ratified. Individuals who are residents of Malta are 
                eligible to benefit from Malta’s double taxation treaty network.
                <br>
                <br>
                <b>Labour availability </b><br>
                The Maltese workforce is known for their productivity and 
                outstanding work ethic. The Maltese are well-versed in the 
                English language, multi-skilled and familiar with technology.
                <br>
                <br>
                <b>Social security</b><br>
                All employed and self-employed persons are required to pay 
                Social Security Contributions. The amount of Social Security 
                Contribution (SSC) to be remitted to the Inland Revenue 
                Department depends on the employee’s salary. The SSC amount is 
                to be paid by both the employee and the employer.<br>
                <br>
                <b>Exchange control <br>
                </b>There are no exchange control restrictions.<br>
                <br>
                <b>Import regulations </b><br>
                In general, Malta does not impose restrictions on the import of 
                goods from other countries or groups of countries. Goods 
                imported from non-European Union countries may be subject to 
                import duties in addition to VAT (when applicable). An export 
                licence is required for specified goods.<br>
                <br>
                Malta’s membership in the EU, its strategic location and its 
                growth as a financial centre make it a premier starting point 
                for companies seeking business development in the region. A 
                highly advanced telecommunications network, a skilled workforce 
                and numerous financial incentives makes Malta favourable to 
                international investors and exporters. <br>
