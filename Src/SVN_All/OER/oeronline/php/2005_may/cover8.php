            <img border="0" src="john.jpg" align="left" vspace="2" width="200" height="250"></b></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">he 
            year 2004 was a productive year for Oman International Bank (OIB) 
            with several major achievements. The bank, which started the year 
            with celebration of its 20th Anniversary, focused on consolidating 
            its position in the local market, increasing its market share, and 
            enhancing asset quality while reducing operating expenses during the 
            course of the year. <br>
            <br>
            With the corporate book clean-up exercise a thing of the past, the 
            bank has managed to consolidate its position, as promised by John 
            Carlough, the bank’s CEO, when OER met him last year. “Not only has 
            the clean-up exercise been successful, we have had very positive and 
            encouraging results,” Carlough had claimed then. Indeed, the bank’s 
            performance speaks for itself. <br>
            <br>
            As a result of strong growth in the loan book, funded by low cost 
            deposits, the bank’s net interest income increased from RO24.698 
            million in 2003 to RO27.493 million in 2004, a growth of 11.3 per 
            cent. ‘‘Business continues to perform well, driven by the excellent 
            momentum in retail, corporate and treasury activities,’’ maintains 
            chairperson Reem Omar Zawawi in her report to the shareholders. <br>
            <br>
            To be sure, the bank’s other income for the year 2004 of RO8.898 
            million showed a growth of 13.2 per cent over the previous year, 
            largely the result of increased volume of retail and corporate 
            business. ‘‘Our efforts to reduce operating expenses continue with 
            several cost rationalisation initiatives undertaken during the 
            year,’’ says Zawawi. The results reflect a favourable trend with the 
            operating expenses reflecting a reduction of 8.1 per cent from the 
            previous year. As a consequence, the bank’s cost to income ratio 
            declined to 47.5 per cent for 2004 as compared to 56.0 per cent for 
            the previous year. <br>
            <br>
            Accordingly, the OIB’s operating profits increased from RO14.911 
            million in 2003 to RO19.273 million in 2004, a strong growth of 29.3 
            per cent. ‘‘Our commitment to improve asset quality and thereby 
            reduce the provision for loan impairment continues with the specific 
            provision for loan impairment for the year decreasing by 29.6 per 
            cent to RO4.410 million, compared to the previous year,’’ states 
            Zawawi. <br>
            <br>
            In addition, the bank’s emphasis of continued improvement of 
            customer service remained a major priority for the year. The bank’s 
            focus on recovering non-performing loans continues with a bottom 
            line impact of RO3.273 million for the year. OIB’s total 
            non-performing loans decreased from RO79.3 million as of December 
            31, 2003, to RO75.5 million as of December 31, 2004. <br>
            <br>
            ‘‘Our strategy of improving the loan loss coverage ratio continues 
            and the ratio improved significantly from 83.6 as of December 31, 
            2003 to 91.2 per cent as of December 31, 2004. The coverage ratio 
            increases to 101.3 per cent after considering the value of the 
            underlying securities,’’ states Zawawi. Accordingly, the bank posted 
            a net profit of RO14.085 million for the year 2004, an increase of 
            6.5 per cent over the previous year. <br>
            <br>
            In the light of these results, the bank’s board proposed a cash 
            dividend of 21 per cent to be paid in 2005, subject to the approval 
            of the shareholders. Overall the year was a rewarding one for the 
            Bank’s shareholders. In addition to the payment of the cash dividend 
            of 21 per cent on the face value of the share, the bank’s share 
            price also appreciated by 43.5 per cent. <br>
            <br>
            The bank’s Loans and Advances stood at RO454.818 million as at 
            December 31, 2004, compared to RO388.024 million as at December 31, 
            2003, representing a growth of 17.2 per cent year-on-year. Total 
            Deposits at the year-end stood at RO571.369 million compared to 
            RO472.730 million at the same time last year, representing a growth 
            of 20.9 per cent year-on-year. The bank’s management informs that it 
            continues to focus on generating low-cost deposits. <br>
            <br>
            <b>Industry overview</b><br>
            The banking industry in Oman continued to demonstrate positive 
            growth, with a total deposit growth of 7.9 per cent and a total 
            lending growth of 6.1 per cent. While the Personal Loan segment grew 
            by 8.8 per cent, the Corporate Loan segment grew by 4.4 per cent. 
            Interest rates continued their declining trend throughout the first 
            three quarters of the year, with a reversal of this trend during the 
            last quarter of the year.<br>
            <br>
            <b>Outside Oman</b><br>
            Internationally, in India, OIB’s strategy of focusing on recovery of 
            problem loans, limit asset growth, improve cost of funds and reduce 
            operating costs continues. The results continue to be encouraging 
            with a strong improvement in the operating income. However, this has 
            been offset by the revaluation loss on the investments in government 
            securities, as required under International Accounting Standards, as 
            a result of movement in market interest rates during the year. <br>
            <br>
            The operations in Pakistan continue to be closely managed. Changes 
            in lending policies have been implemented to further limit 
            concentration risk. The bank continues to focus only on top quality 
            loans and has a clean loan portfolio. The Pakistan operations’ 
            income has also been offset by the revaluation loss on the 
            investment in term finance certificates, as required under 
            International Accounting Standards. The bank is currently exploring 
            various options to meet the increased capital requirements imposed 
            by the State Bank of Pakistan and the regulators have given it an 
            extension of time up to June 30, 2005.<br>
