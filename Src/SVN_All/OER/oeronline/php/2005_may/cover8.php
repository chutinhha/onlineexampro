            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">
            Despite making an incremental jump of 2.14 per cent in revenues, 
            from RO43.66 million in 2003 to RO44.59 million in 2004, Oman 
            International Bank dropped a notch in The OER 20 rankings. However, 
            as proof of the bank’s solid fundamentals, look no further than its 
            profit ranking – No. 2 for the last three years, with a 13.22 per 
            cent jump in net profits this year</font></b></p>
