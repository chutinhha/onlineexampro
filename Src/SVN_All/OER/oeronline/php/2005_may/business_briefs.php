                    Oman and Qatar have signed an agreement for the 
                    establishment of a joint investment company at a cost of 300 
                    million Qatari riyals ($82.5 million; RO31.73 million). The 
                    agreement was signed by Qatar’s Deputy Prime Minister, 
                    Minister of Energy and Industry Abdullah bin Ahmad 
