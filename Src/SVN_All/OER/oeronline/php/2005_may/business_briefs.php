                    In sync</b><br>
                    Michael Kutschenreuter, Chief Financial Officer of Siemens 
                    Communications, visited the Sultanate recently. Excerpts 
                    from an exclusive interview with <b><i>OER</i>’</b>s <b>
                    Rafiah Salman Al Talei</b><br>
                    <br>
                    <b>
                    <img border="0" src="bs_br1.jpg" align="left" hspace="4" width="120" height="131">Welcome 
                    to Oman, Michael. What brings you here, and is this your 
                    first visit to the country?</b><br>
                    Thank you. No, it isn’t my first visit t Oman – I have been 
                    a regular visitor to the Sultanate, and come here at least 
                    once or twice every year to discuss company matters and 
                    relations. I simply love this country and the people. My 
                    visit precedes German Chancellor Gerhard Schröder’s visit to 
                    the country.<br>
                    <br>
                    At Siemens, we have a strong commitment with to the 
                    Sultanate. We have nurtured strong and lasting relations 
                    with Oman Telecommunications Company (Omantel) and Oman 
                    Mobile through our joint projects. One of our latest 
                    projects is the broadband project – an important project for 
                    Oman as it allows Omantel to provide broadband services for 
                    schools, cyber cafés, leading corporates, hotels, houses and 
                    other places.<br>
                    <br>
                    <b>What are the challenges that multinational 
                    telecommunications companies like Siemens face here in Oman?</b><br>
                    Omantel and Oman Mobile are well– organised companies with 
                    skilful staff. Identifying customer trends is a great 
                    challenge – not only in Oman, but anywhere in the world. We 
                    must know the customer’s needs and fulfil them before they 
                    change.<br>
                    <br>
                    <b>Will the topography of Oman be an obstacle for expanding 
                    telecommunications network?</b><br>
                    I do not consider that a serious problem. It can be done 
                    easily using the latest equipment. For many years Siemens 
                    has been deploying, successfully, telecommunication 
                    infrastructure .<br>
                    <br>
                    <b>What do you think is a right time to privatise?</b><br>
                    Privatisation must happen at a time when investors are ready 
                    for it so as to ensure success and a smooth transition. This 
                    is the way it has happened in many countries in Europe and 
                    other places in the world. It will be useful to learn from 
                    their experiences but it will be more useful to learn from 
                    the experience of the non-successful privatisation projects 
                    around the world. In Oman, privatisation is bound to be 
                    successful as a sound telecommunications infrastructure is 
                    in place and work is well-organised. <br>
                    <br>
                    <b>Is the number of telecom firms in Oman sufficient for the 
                    market?</b><br>
                    The number of subscribers is increasing every day, and the 
                    telecommunications industry is evolving rapidly. Many 
                    additional services are being created. A solid competition 
                    will allow more services to be offered at reasonable and 
                    competitive prices.<br>
                    <br>
                    One new service is the Asynchronous Digital Subscriber Line 
                    (ADSL), which is like a gateway for future technology. 
                    Through this service, new services such as high speed 
                    Internet, online gaming and video-on-demand are possible. I 
                    am optimistic regarding the popularity of ADSL in the 
                    Sultanate – I know that there are only around 54,000 
                    Internet subscribers in the country, but I am quite 
                    confident that this number will grow in the future.</font></p>

