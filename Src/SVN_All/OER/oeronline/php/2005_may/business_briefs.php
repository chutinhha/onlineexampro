                    Port Services Corporation (PSC) will spend RO100 million on 
                    an expansion plan and creation of new facilities. According 
                    to Saud bin Ahmed al Nahari, executive president of PSC, the 
                    money will be used for building new breakwater, additional 
                    berth and storage areas, logistics centre and for setting up 
                    of a new passenger terminal. The port will take 
                    three-and-a-half to four years to complete, he added. <br>
                    <br>
                    Of the amount, RO60 million will be from the government for 
                    creating infrastructure facilities at the port. The 
                    remaining will be invested by the corporation for setting up 
                    superstructure and other facilities. The sources of funds 
                    for the proposed expansion will be soft loan and long-term 
                    financial assistance from regional lending institutions, 
                    revealed Nahari. PSC operates and manages the Sultan Qaboos 
                    Port.</font></p>

