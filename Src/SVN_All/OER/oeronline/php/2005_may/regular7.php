            <p align="justify"><font face="Verdana" size="2" color="#C0C0C0">:: 
            Car Review</font></p>
            <p align="justify"><font color="#FF0000" face="Verdana">
            <font size="4">You Small wonder</font></font><font face="Verdana" size="1"><br>
            <br>
            <b>If there ever was a four-wheeled version of the phrase ‘small is 
            beautiful’, this is it! Vicky Kapur takes the new Kia Picanto for a 
            jaunty ride around town</b></font></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im35.jpg" align="left" hspace="5" width="200" height="121">Call 
            it the big little car! Kia Picanto 1.1L, the latest wonder-on-wheels 
            to hit Oman roads from the stables of the Saud Bahwan Group, is an 
            entry-level car in the huge small-car segment in the Sultanate. And 
            by the sheer look of it, it is all set to snatch a massive chunk of 
            the first-time car buyers’ cake from some of its more established 
            rivals. <br>
            <br>
            But before I get all limerick-y about its looks, let me give you a 
            background, though. The five-door hatchback does not pretend to be a 
            snob. I reckon that it will appeal mostly to the young—and, of 
            course, young-at-heart—first-time native car buyers and the loads 
            and loads of second-hand car buyers. Who wouldn’t want to own a 
            brand new, though smaller, vehicle at the price of a bigger but aged 
            automobile? <br>
            <br>
            The Kia Picanto is what car experts will call the basic, 
            value-for-money car. No over-priced frills, no expensive jazz – just 
            a pure means of transportation to go from Point A to Point B. But 
            one that will make you look good in it. Starting at RO2,095 (the 1.1 
            LX), the Picanto undercuts some of the other cars in its class 
            significantly and, to top it, is better equipped.<br>
            <br>
            Kia is known to make tough, dependable cars with uncomplicated 
            components – even below the hood. While the engine is not 
            technically over-complex, where the Kia Picanto scores the max is in 
            its attention to detail – something that its other Korean peers 
            sorely lack. In fact, the Picanto matches and even beats many 
            entry-level European cars in this class. <br>
            <br>
            <b>The outdoors</b><br>
            <img border="0" src="im36.jpg" align="right" hspace="7" width="200" height="154">Kia must have had employed some daringly bold—and rather 
            young—stylists for the Picanto. The brash trapezoid radiator grille 
            (body-coloured in the 1.1 EX PWR variant that OER tested) makes the 
            car appear to be happily grinning. For most of us who thought that 
            city cars should either be cute (read: meek and docile) or 
            aggressive (read: towering 4x4s), the Picanto is a welcome 
            deviation. It’s cuddly yet sturdy, friendly yet indomitable. And at 
            the risk of repeating myself, it isn’t a snob! <br>
            <br>
            The Picanto has been tipped to be one of Kia’s first standalone 
            designs. It manages to be modern and individual without being overly 
            fussy. The Picanto has a contemporary design, with mini-MPV 
            proportions, and a distinctively bold front fascia. The body-coloured 
            rear garnish and tinted windshield, door and tailgate glasses give 
            it a smart, invincible look. <br>
            <br>
            The top-of-the-line variant that OER test-drove was also fitted with 
            body-coloured bumpers, handles, mirrors and other accompaniments 
            apart from a host of other utilitarian goodies such as the central 
            door lock w/tailgate, rear wiper and washer, sliding sun visor, and 
            full-wheel covers, which added to the sporty look of the Picanto, 
            which looks fresh, and draws inquisitive glances from passers by.
            <br>
            <br>
            <b>The indoors</b><br>
            Although the interiors may not be as rich as some of the high-end 
            cars, the Picanto beats most of its rivals in class on the quality 
            and size of the cabin. To talk about size, the extra millimetres in 
            width not only grant the Picanto with much better looks, they 
            provide a significant advantage in terms of elbowroom. <br>
            <br>
            In general, the Picanto feels more spacious; it also has more useful 
            cubbyholes for all your (and your kids’) bits and pieces. It is easy 
            to get comfortable in the driver’s seat of the Picanto, the 
            switchgear is sensibly laid out and the view out of the car is 
            clear, though the result of a sharply sloping nose is your inability 
            to see the very front of the car. <br>
            <br>
            For this price level, the Picanto is amazingly well equipped. It 
            comes loaded with a radio and cassette player with four speakers, 
            rooftop antenna, AC with filter, heated rear glass, luggage under 
            floor box (trunk), tachometer and digital clock, day/night inside 
            rear-view mirror, glove box and door map pocket, ash tray and cigar 
            lighter, and a one-piece rear bench folding seat. <br>
            <br>
            Thanks to the mini-MPV profile, the Picanto has plenty of space 
            inside, despite a small footprint. The ergonomics cannot be faulted, 
            and it feels well put together too. However, I must add that the 
            materials used in the cockpit could be better, but then nothing is 
            free in this world, and Kia seems to have done a wonderful job of 
            keeping the car sub-RO2,095.<br>
            <br>
            Rear seats are really only for children, or close friends on a short 
            journey, but they’re adequate for a car of this size and type. Shame 
            the rear seat doesn’t slide forwards on its base to give the option 
            of more luggage space, however, as the boot is very small, but it 
            does split, fold and tumble to form a flat load bay area. <br>
            <br>
            <b>The performance</b><br>
            Once on the move, the Picanto displays remarkable agility and 
            control for a mere 1.1L engine. The 64 HP @5500 rpm power and the 
            9.9 kg-m @2800 rpm torque feel much more than that – perhaps because 
            of the 5-speed manual transmission although a 4-speed automatic 
            transmission is also available. A turning radius of 4.6 m gives the 
            Picanto enviable on-road agility and a very desirable 
            near-effortless parking ability. <br>
            <br>
            The Kia is well screwed together, with no annoying squeaks or 
            rattles. The controls are quite slick, especially the gearbox and 
            clutch. The brakes inspire confidence, with a firm pedal full of 
            feel. Despite the fact that the Kia Picanto has been designed with 
            urban living in mind, it copes admirably with motorway use. Its 
            damping is well judged, soaking up ruts and bumps with ease. Unlike 
            many city cars, the Picanto proved to have excellent stability and 
            resistance to crosswinds. <br>
            <br>
            Its steering is light but direct, its suspension agile enough to 
            corner with confidence and its gearbox accurate for punting it along 
            quickly. It’s most at home in city traffic, nipping across lanes, 
            cutting through narrow gaps and jumping away from the lights, but 
            it’s adept for faster progress along country lanes and holds its own 
            on the motorway. <br>
            <br>
            All-round visibility is remarkable, the driver’s seating is well 
            elevated, and all the controls are easy to locate and operate. The 
            engine and gearbox are in harmony with each other, and the compliant 
            ride and safe handling in the Picanto is much appreciated. Its 
            on-road stability is commendable too.<br>
            <br>
            The Picanto’s cabin is well insulated from wind and tyre noise, even 
            at high speeds, though the engines can get loud and raucous when 
            pushed hard. Passengers in the rear will do well to remember that a 
            journey in the Picanto is not meant to be an advertisement for 
            first-class luxury travelling. Having said that, the rear seats are 
            also not as cramped as in some of the other cars in the same class.
            <br>
            <br>
            A ride in the Picanto is of acceptable quality at lower speeds but 
            can get roly-poly during quick cornering, and bouncy on rougher 
            roads. It doesn’t jar badly over broken surfaces, however, and its 
            bounciness seems more like a sign of a fun character than a 
            technical failing.<br>
            <br>
            <b>The safety</b><br>
            The Picanto bagged four stars in the Euro NCAP crash tests for child 
            protection (with Kia’s approved child seats), but managed a mere 
            three stars for occupant protection, and only one star for 
            pedestrian protection. It does feel reasonably solid, however—as 
            cars in this featherweight class go—and is certainly more 
            substantially built than some of its older rivals. Full credit to 
            Kia for making side-impact beams, fire extinguisher and child safety 
            lock standard equipment. <br>
            <br>
            <b>The finishing line</b><br>
            The Picanto is going to meet all basic needs, even if it doesn’t 
            boast the latest in digital climate control technology or telecoms 
            gadgetry. Comfort-wise, it’s fine in the front even for tall 
            occupants, though the broader-in-the-beam could find the cabin 
            rather narrow. Performance-wise, the Picanto is as good as it gets – 
            in this class and price-level, of course!<br>
            <br>
            <i>Road test courtesy Saud Bahwan Automotive, the sole 
            representatives of Kia vehicles in Oman</i></font></p>
