            <font face="Verdana">Making waves!</font></font><font face="Verdana" size="1"><b><br>
            </b><br>
            <b><i>OER</i> profiles the never-say-die persona of Wael Ahmed Al 
            Lawati, the acting CEO of Waterfront In vestments—promoters of The 
            Wave project—a project that is making waves in the country</b><br>
            <br>
            <img border="0" src="lawati.jpg" align="left" width="200" height="269"></font><b><font face="Verdana" size="3">H</font></b><font face="Verdana" size="1">is 
            first big challenge in life was to learn Arabic at the age of 12. 
            ‘‘Since my early schooling was outside Oman, when we came back for a 
            short period to Oman, I had to learn Arabic at the age of 12,’’ he 
            says. ‘‘What this meant was that, every morning, I had to leave my 
            colleagues in the sixth grade and head over to the miniature chairs 
            of first grade.’’ This, of course, was both embarrassing as well as 
            intimidating for him. <br>
            <br>
            However, he stuck to his guns, and soon mastered the language. 
            ‘‘Within a year, I was able to rejoin my classmates in seventh grade 
            and be integrated into the class with minimal difficulty,’’ he 
            exclaims. This was perhaps the first taste of success for Wael Al 
            Lawati, today the acting CEO of Waterfront Investments, the 
            government company promoting the much-talked-about billion-dollar 
            project, The Wave, one of the most prestigious and perhaps the 
            largest tourism development resort project in the country. <br>
            <br>
            Determined, daring, dynamic and a thorough perfectionist, Lawati is 
            a true entrepreneur. Whether it’s construction and planning, 
            drafting of the company’s vision, heading as the CEO of a firm, 
            designing the logo of a project, or even playing mommy and daddy to 
            small kids, Lawati puts his heart into it and believes doing 
            everything passionately. With the right frame of mind and a perfect 
            blend of all the ingredients needed for success, Lawati hopes to 
            accomplish much more. His success secret? Get up early, work with 
            passion and enjoy even if work becomes torturous.<br>
            <br>
            <b>Around the world</b><br>
            Born in Kuwait, Wael Ahmed Al Lawati has been lucky to have lived in 
            a number of countries. His father, being an ambassador in the 
            Foreign Service, helped Lawati to set his foot in many countries of 
            the world – ‘‘Kuwait, India, China, Malaysia, China, Somalia and the 
            UK,’’ he rattles nonchalantly. However, although he was fortunate to 
            travel around the world, his schooling was not all that easy as he 
            had to change schools nearly every year and adapt himself to 
            different curricula.<br>
            <br>
            Having attended many international schools, Lawati is a 
            well-educated man. In Oman, he went to the Sultan School. After his 
            high school certificate, which he obtained in a derelict school in 
            Somalia, Lawati headed to the UK where he majored in mechanical 
            engineering and graduated in 1993. To augment his technical 
            training, Lawati attended executive education programmes at Harvard 
            Business School and IMD in Lausanne. “Both these courses were 
            invaluable in terms of expanding my knowledge of business and 
            management concepts. It helped me to benchmark the differences 
            between the corporate world at large and that in Oman,” recalls 
            Lawati.<br>
            <b><br>
            Sphere of success</b><br>
            Lawati has had a very successful professional life till date. He 
            started his career with the Petroleum Development of Oman (PDO), 
            where he was initially assigned to the oilfields in construction and 
            planning. Thereafter, he returned to Muscat and worked in projects, 
            major contracts and finally in the ‘Change’ team, which was set up 
            to help formulate and implement a new business model and way of 
            working. <br>
            <br>
            In addition to this, Lawati was involved in various high-profile 
            initiatives including the drafting of the new company vision, 
            restructuring and re-engineering. “One of the initiatives that left 
            an indelible mark on my perspective on management was heading a 
            taskforce that was mandated with reviewing means of raising 
            motivation in the company,” says Lawati. “This required an in-depth 
            review of the corporate culture and means of transforming this 
            multi-cultural organisation. I feel fortunate to have been a part of 
            PDO’s efforts in the development of Oman and feel privileged to have 
            worked in an organisation with a huge talent base,” he adds.<br>
            <br>
            Lawati believes in introspection. In 2003, after having completed 10 
            years of professional life, he took time out to think and plan his 
            future. “I wanted to reflect and think where I wanted to be and how 
            I wanted to spend the next 10 years of my professional life,” he 
            stresses. “The decision was to either accept a posting with Shell, 
            internationally, or help establish a new government-owned tourism 
            company to manage the waterfront project (later renamed as The 
            Wave). <br>
            <br>
            ‘‘While I had set my self a goal of ensuring that I had an 
            international career exposure, the excitement I felt about setting 
            up a new company to manage the billion dollar project was 
            overwhelming and so I decided to stay in Oman and modestly help in 
            the development of the burgeoning tourism sector,” he adds. With 
            hindsight, Lawati has no regrets. On the contrary, he is a man full 
            of life, who believes in taking each day as a challenge. “I still 
            spring out of bed every morning which is always a test of how 
            motivated one is even if I know the day will be full of several 
            challenges,” he says. <br>
            <br>
            Pitching The Wave project to investors regionally and 
            internationally has been a very rewarding experience for Lawati, 
            even though it has meant long working hours and a huge imbalance in 
            his work and personal life. It’s a keen interest in the new sector 
            and an understanding wife that helped Lawati make his professional 
            journey enjoyable. Lawati also gives credit to his parents for 
            teaching him the ethics of work. His mother, besides being a 
            homemaker, was a school headmistress, and both his parents have 
            played an important role in shaping him into a successful CEO.<br>
            <br>
            Besides his very successful professional life, Lawati has other 
            feathers in his cap. He is the Secretary General of the Oman Society 
            of Engineers. He deserves due credit for attaining this post in a 
            short span of time – he joined the Board in the latter half of 2003. 
            Lawati is also a member of the Young Arab Leaders and is in the 
            process of establishing the Muscat chapter in addition to board 
            membership of other companies.<br>
            <br>
            <b>Management mores</b><img border="0" src="lawati1.jpg" align="right" width="200" height="213"><br>
            “I always feel it is superficial to ask managers about their style 
            since most people overrate their style. My staff should describe my 
            style since they see it and are affected by it or are even the 
            victim of it,” says a jovial CEO. Lawati strives to be fair and 
            firm. “I can usually stay calm and collected during the most 
            tumultuous times which, at times, is perceived as being indifferent, 
            but it is my way of ensuring that I can keep performing in tough 
            times. Delegating has always been difficult for me and I am acutely 
            aware of the need to develop this since there is obviously a limit 
            to what I can do myself,” he adds. <br>
            <br>
            Lawati believes in group-work, free of hierarchical menace. For him, 
            his stakeholders are his partners and he aspires to make his working 
            environment free of boundaries. However, this is not always easy for 
            a CEO as, at times, people take advantage of this, but Lawati still 
            believes in the need to sensibly remove barriers between partners. 
            This is how he functions at The Wave. “I’m applying my philosophy 
            here at The Wave project. All of us—the designers, consultants, 
            investors and others—work with open agendas, but also with an 
            agreement on where our interests might differ,” he explains.<br>
            <br>
            <b>The Wave</b><br>
            Lawati is of the view that Oman has a lot of tourism potential, and 
            along with the associated real estate sector, it is albeit at a 
            crossroads, in terms of the many decisions that need to be taken or 
            implemented. “We have to identify what type of tourism we want to 
            develop, the type of products that best fit within our strategy, the 
            appropriate pace to ensure both local acceptance of this change and 
            the preservation of our heritage,” he elaborates. <br>
            <br>
            ‘‘Having started late, Oman has a lot of catching up to do,’’ he 
            points out, but cautions that ‘‘that should not make us take hasty 
            decisions or force us to emulate what others are doing since we need 
            to differentiate ourselves in the cluttered world of destinations.’’ 
            Despite the slow start, Lawati is buoyant about Oman’s tourism 
            prospects. ‘‘I think I would be patronising if I started listing 
            what Oman can offer since any visitor or resident of Oman can see 
            how gifted this land is and its sheer potential.’’<br>
            <br>
            Hard work pays, and Lawati is a proud man to be associated with The 
            Wave project. “One of the key achievements of The Wave project is 
            that it has established itself despite being at this early stage of 
            development. The project has become synonymous with Oman’s 
            development of its tourism sector. No (news) article locally or 
            regionally mentions Oman and tourism without mentioning The Wave. 
            Furthermore, the road shows and PR efforts for The Wave have created 
            a growing interest in the region to explore opportunities in this 
            sector,” he explains.<br>
            <br>
            Lawati expects The Wave project to be completed within the scheduled 
            timeframe. Of course, The Wave, being a major undertaking, requires 
            many legislative and technical issues to be tackled. “We are 
            continuously increasing the tempo and so the appointment of the key 
            consultants will happen with April and site preparation starts in 
            May. The choice of partners has been done very diligently, and I am 
            fortunate to work with the professional team at Majid Al-Futtaim 
            Investments who have demonstrated commitment to the project from the 
            onset. I am certain that the project will continue to raise the 
            interest of the residents of Muscat and visitors for the diversity 
            of the offering and the accessibility”, Lawati says hopefully.<br>
            <br>
            ‘‘Our measure of success will be that any visitor to Muscat will 
            consider a visit to the Wave as essential as a visit to Mutrah or 
            the souq. If we can get to that status, rivalling these icons that 
            have withstood the test of 400 years then we would have succeeded,’’ 
            he says. A true stretch target, indeed.<br>
            <br>
            <b>Perfectionist to the core</b><br>
            Lawati is, indeed, a perfectionist, and he makes no bones about it. 
            “I endeavour for perfectionism in the office. When working on the 
            initial project branding, I spent endless hours working with 
            designers on the project name and logo. I had to sit for several 
            hours with the designer just to decide the colours of the logo, and 
            yes, I did go through hundreds of shades of blue. My philosophy was 
            that to create a world-class brand for an international project. The 
            next phase of detail branding will require even more mental and 
            visual torture, but fortunately, it is an area I enjoy,” he gleams. 
            Despite being a workaholic and a perfectionist at office, Lawati 
            loves to laze around at home. A totally different person, Lawati 
            admits that he delays and ignores things that need to be done at 
            home. <br>
            <br>
            <b>A family man</b><br>
            Despite all the work and office stress, Lawati is a very homely 
            person. “I have been happily married to my wife Alia Al-Farsi for 11 
            years. She is an active artist, and is currently pursuing a 
            full-time MBA in the UK. This means I have to play mommy and daddy 
            to my two kids – Haitham, who is 9, and Deema, who is 5. My 
            favourite time of the week is not choosing logo shades, but going 
            for the weekly family lunch at my parents’ place. Meeting the whole 
            family—my four sisters, in-laws, and nephews—is something that I 
            look forward to. And, yes, I try to make time for socialising with 
            friends”, he grins.<br>
            <br>
            Even though Lawati has visited many countries around the world, he 
            is one restless individual. He still yearns to travel. “I love 
            exploring countries or cities with history and places with soul,’’ 
            he says. ‘‘Since I am spending my time with architects at the 
            moment, I have started using all these exotic descriptions,’’ he 
            adds with a twinkle in his eyes. <br>
            <br>
            ‘‘Some cities that have stirred a real emotion in me are Florence in 
            Italy, and Bergen in Norway,” he says. Besides travelling, Lawati 
            loves reading, and that is one hobby that he has not given up 
            despite his work commitments. Management books and science fiction 
            novels are his favourites as, according to him, ‘‘both address 
            imaginary worlds’’! That, and trying new cuisines is a true passion 
            for him. “My waist line is sure suffering due to that,” he chuckles.<br>
            <br>
            <b>Focus Oman</b><br>
            Oman has come a long way from the time Lawati entered the 
            administrative arena. It is moving towards its goal of being a 
            non-oil dependant economy. “What every visitor remarks when they 
            visit Oman is how impressed they are by what has been achieved in 
            this short period and for a country with limited resources. It is 
            clear the wisdom of His Majesty has helped create a true modern day 
            miracle,’’ he says.<br>
            <br>
            As citizens or residents of Oman, we occasionally take things for 
            granted and believe that diversification is an easy step. ‘‘On the 
            contrary,’’ claims Lawati, ‘‘as any manager knows, they are usually 
            very few easy answers or choices. The development requirements of 
            our young population are huge. This needs to be balanced with the 
            creation of our future, which will rely on declining oil. Being in 
            the tourism sector, I feel that the visibly growing momentum in this 
            sector will help raise our contribution to GDP and, equally 
            importantly, job creation,” affirms a patriotic Lawati. Insha Allah! 
