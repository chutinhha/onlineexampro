<br>
An important debate among stock market investors is whether the market is 
efficient – that is, whether it reflects all the information made available to 
market participants at any given time. Efficient Market Hypothesis (EMH) hold 
that stocks are always in equilibrium and that it is impossible for an investor 
to consistently beat the market. If markets are efficient, stock prices will 
rapidly reflect all available information. In theory, there are generally three 
types of market efficiencies: weak form efficiency, semi-strong form efficiency 
and strong form efficiency.<br>
<br>
The weak form of the EMH states that all information contained in past price 
movements is fully reflected in current market prices. If this were true, then 
the information about recent trends in stock prices would be of no use in 
selecting stocks. The fact that a stock has risen for the past three days, for 
example, would give us no useful clues as to what it will do today or tomorrow. 
Consequently, weak-form efficiency implies that any information that comes from 
past stock prices is rapidly incorporated into the current stock price.<br>
<br>
The semi-strong form of EMH states that current market prices reflect all 
publicly available information. Therefore, if semi-strong form efficiency 
exists, it would do no good to pore over annual reports or other published data 
because market prices would have already adjusted to any good or bad news 
contained in such reports. Another implication of semi-strong form efficiency is 
that stock prices will respond to any information only if it is different from 
what had been expected. If a company announces, say, a 40 per cent increase in 
earnings and if that increase is what analysts had expected, the announcement 
should have little or no effect on the company’s stock price. On the other hand, 
the stock price would fall if analysts had expected earnings to increase by more 
than 40 per cent.<br>
<br>
The strong form of the EMH states that current market prices reflect all 
pertinent information, whether publicly available or privately held. If this 
form holds, even insiders would find it impossible to earn abnormal returns.<br>
<br>
<b>Implication</b><br>
Stock prices do seem to reflect public information, most stocks appear to be 
fairly valued. This does not mean that new developments could not cause a 
stock’s price to soar or plunge, but it does mean that stocks in general are 
neither overvalued nor undervalued – they are fairly valued and in equilibrium. 
If the EMH is correct, it is waste of time for most of us to analyse stocks by 
looking for those that are undervalued. If stock prices already reflect all 
publicly available information and hence are fairly priced, one can beat the 
market only by luck and it is difficult, if not impossible, for anyone to 
consistently outperform the market averages.<br>
<br>
However, our tests of market behaviour have shown that the EMH is in its weak 
and semi-strong forms valid. That is, no one can make abnormal returns by simply 
examining past price performances or using publicly available information. 
However, people such as corporate officers who have inside information can do 
better than the averages. Also, some investors may be able to analyse and react 
more quickly than others to releases of new information, and these investors may 
have an advantage over others. However, the buy-sell actions of those investors 
quickly bring market prices into equilibrium and any abnormal returns quickly 
disappear. So should my client buy the stock? You tell him!<br>
