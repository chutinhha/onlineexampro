this year, one of my clients approached me with an investment opportunity. He 
had received a tip that one of the local companies was about to sign a contract 
that was supposed to boost its stock price. The stock was already up by more 
than 60 per cent The company had not announced the deal till then, and he 
expected the stock to soar further. He wanted to buy the stock and asked for my 
