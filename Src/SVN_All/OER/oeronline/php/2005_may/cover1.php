            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>The 
            good news for Shell Oman Marketing is that it has, for the fourth 
            year in a row, managed to retain the numero uno position in The OER 
            20 rankings. The not-so-good news for it is that it is in serious 
            danger of being dislodged from that slot next year – just short of 
            completing half-a-decade of leading the coveted ranking. Will it, 
            won’t it?</b></font></p>
            <p align="justify" dir="ltr">
            <img border="0" src="irshad.jpg" align="left" vspace="2" width="200" height="250"></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">I</font><font face="Verdana" size="1">t’s 
            a performance worthy of critical acclaim. Despite a RO20-odd million 
            drop in its 2004 income over 2003, Shell Oman Marketing Company 
            (Shell Oman) still leads the pack of Oman’s top listed companies. 
            The entry of Al Maha Petroleum Products Marketing Co. into the 
            Muscat Securities Market (The OER 20, 2005 rank: 6) of course did 
            not budge the giant from its top slot. Neither did an almost RO6 
            million jump in revenues of the second ranked BankMuscat, which is 
            still more than RO13 million behind the oil giant in terms of annual 
            income. However, to put things in perspective, that lead has shrunk 
            from approximately RO40 million last year. <br>
            <br>
            Shell Oman has delivered RO5.3 million net profit for 2004 compared 
            to RO5.6 million in 2003. Considering the intensely challenging 
            environment the company is operating in, this is a commendable 
            performance. Following the dividend policy adopted by Shell Oman for 
            a full payout of the net profit, the company’s board recommended a 
            final dividend of RO5.3 million – i.e., 530 baisas per share. <br>
            <br>
            This, together with the ‘one-off’ interim dividend of RO4 million 
            paid out in November 2004, represents a total dividend declared for 
            2004 of RO9.3 million, taking the total dividend disbursement to the 
            shareholder to almost RO1 per share in the year. This level of 
            dividend generates a dividend yield of 18.2 per cent on the share 
            price as at December 30, 2004. This is 111 per cent higher than the 
            dividend yield delivered for 2003. In addition, the return on 
            capital employed has risen to 24 per cent.<br>
            <br>
            ‘‘Our overall performance has been good. We were indeed expecting a 
            drop, and therefore had a strategic plan in place to leverage the 
            available opportunities without losing efficiency in the different 
            parts of our business functioning, including the four market areas 
            of Aviation, Commercial, Retail and Lubricants,’’ says Irshad Moosa 
            Al Lawati, Shell Oman’s managing director. ‘‘Not only that,’’ he 
            adds, ‘‘we also tackled the cost side of the business through 
            initiatives that addressed the overall management of operating 
            costs, as well as the unit cost – which is one of our key 
            performance indicators. So, while growing the volumes, reducing unit 
            cost gave us the economies of scale, which eventually added to the 
            bottomline. Of course, the bottomline also reflects the positive 
            effects of lower depreciation charge and improved working capital 
            management.’’<br>
            <br>
            <b>Overview</b><br>
            The rise in crude oil prices has had a major impact on the overall 
            economic sentiment in the country as well as on the global scene. 
            While this has boosted the Sultanate’s income from oil exports, it 
            has also resulted in significant import price inflation. ‘‘The 
            country’s business environment is positive because Oman’s economy is 
            growing well, taking into account the rising crude oil prices,’’ 
            says Al Lawati. ‘‘That provides the opportunity to support major 
            infrastructure projects, which adds to the demand in our industry,’’ 
            he claims. ‘‘Coupled with that, the government’s focus—along with 
            that from other stakeholders in the country—on developing tourism 
            opens immense opportunities for FDI.’’ <br>
            <br>
            However, the intensity of the competitive environment was sustained 
            again this year, leading to tighter unit margins as well as a 
            detrimental extension in sales credit terms for the company. ‘‘The 
            market has three very keen players – two with a legacy while the 
            third one is a relatively new player, coming with the privilege of a 
            six-year embargo on our growth in the Retail sector, which I believe 
            has helped it secure enough footing in the market in order to be in 
            a competitive position. Competitive rivalry has only been 
            increasing, which squeezes margins,’’ comments Al Lawati.<br>
            <br>
            In the Aviation business, Shell Oman was affected by the loss of 
            some core business at the beginning of the year, but managed to 
            secure new business with other customers in the meantime. Following 
            the decision of the Ministry of Oil &amp; Gas to move from the maximum 
            ceiling price-based system used in the past to a Platts-based 
            mechanism, total commercial aviation industry volumes have 
            increased. ‘‘The result of that change has been quite positive in 
            making the pricing transparent, which has contributed to overall 
            growth in commercial aviation traffic in Oman,’’ explains Al Lawati.<br>
            <br>
            While the Retail market benefited from the general growth in the 
            sector as well as the cascade affect from the higher levels of 
            project activity in the country, ‘‘our industry has experienced an 
            aggressive approach to Retail network expansion, which has 
            effectively led to a drop in the average volume throughput per site 
            (a performance indicator that measures the efficiency of retail 
            sites),’’ says Dr Andrew Wood, chairman of Shell Oman.<br>
&nbsp;<br>
            ‘‘Competition for new sites has resulted in more capital intensity 
            in the retail networks and, as a result, a drop in the levels of 
            financial return,’’ he adds. However, as Al Lawati points out, 
            ‘‘Shell Oman’s average volume throughput per site grew, albeit 
            marginally, in 2004, and today stands at 6.7 million litres per 
            annum, close to twice the rest of the industry average.’’<br>
            <br>
            In the aftermath of gasification of power generation in South Oman 
            and the cessation of the once-off Aviation volumes during 2003, the 
            market volume available outside of Retail has shrunk significantly. 
            ‘‘This anticipated dip had been expected to severely impact 2004 
            financial delivery, based on which Shell Oman had projected a 
            substantial decline in net profits for 2004. However, the strong 
            results demonstrate how the company has managed to grow its income 
            base substantially during the year,’’ avers Dr Wood.<br>
            <br>
            The road ahead, however, looks bumpy. An increasingly competitive 
            local market coupled with a dampened global scenario in light of the 
            spiralling crude oil prices, the gasification of Oman’s power 
            stations and the continuing decline in wholesale margins are causes 
            for concerns. The company has been successful in ‘making the most’ 
            from the prevailing circumstances of the market through adoption of 
            a four-part strategy: efficiency, portfolio management, profitable 
            growth and cost control.<br>
            <br>
            <b>Efficiency</b><br>
            The company’s Retail business focused heavily on efficiency, with 
            rigorous marketing activities to improve the customer 
            value-proposition (CVP), targeting full customer satisfaction as 
            well as a clear “dealer value-proposition” to enable the retail 
            dealers to leverage the company’s success and maximise their returns 
            from carrying the Shell brand.<br>
&nbsp;<br>
            ‘‘Efficiency is very important for us as we are a market leader not 
            only in terms of market share, but also branding. We remain the only 
            international brand that blends into an Omani company, offering more 
            value to the customer. Although volumes are key in our retail part 
            of the business, for us to go after volumes alone would compromise 
            our ability to maintain efficiency, which is the backbone of our 
            long-term success,’’ says Al Lawati. <br>
            <br>
            To be sure, the company has resisted the temptation to pursue 
            network growth without viability, which would have a detrimental 
            impact on shareholder value. The result has been an efficient 
            operation with sustained growth and protected integrity of the 
            network.<br>
            <br>
            <b>Portfolio management &amp; growth</b><br>
            A combination of portfolio management and profitable growth has also 
            been the theme in the company’s Commercial business, through a clear 
            mapping out of the customer’s needs. Effective market coverage and 
            timely responsiveness to developments in the market has enabled the 
            conversion of prevailing opportunities into cash. ‘‘Till the 
            end-1990s, we were in a fast growth mode to make sure that our 
            geographical coverage and reach to the customer was proper. The 
            balancing factor now is to ensure this is efficient and 
            sustainable,’’ says Al Lawati. <br>
            <br>
            ‘‘You could open a site on the moon,’’ he quips, ‘‘but tomorrow 
            you’ll realise that there’s no volume there to support the 
            investment made!’’ If the first part of the company’s business 
            development model was to have an efficient geographical coverage, it 
            is now making sure that its offering is robust and sustainable from 
            all stakeholders’ point of view. ‘‘Yes, ALL stakeholders – and that, 
            in essence, is our mission,’’ affirms Al Lawati. <br>
            <br>
            Outstanding performance has been achieved, despite tough competitive 
            hurdles on price discounts and trade terms. While the company’s 
            Lubricants business continued to excel in its volume 
            performance—lubricants blending plant achieved a new high of 57 
            million litres blended—margins remained constrained by the hike in 
            raw material import prices. <br>
            <br>
            ‘‘The local aspect of our lubricants business is much more 
            challenging because it is not always possible to recover our costs 
            from the local market in view of the increasing raw material 
            costs,’’ says Al Lawati. ‘‘However, the local market constitutes 
            just 20 per cent of our lubricants business with the remainder 80 
            per cent of our produce being exported. That’s an opportunity that 
            is growing rapidly, reflected in our significantly growing 
            production volumes.’’ <br>
            <br>
            The Aviation business had a relatively good performance in a 
            “normalised” business situation. ‘‘Generally speaking, we were 
            affected by the loss of business from Gulf Air at the beginning of 
            last year, and had to go back to the drawing board to understand the 
            other alternatives available to us to compensate for that loss. This 
            loss was, fortunately, co-timed with the change in the pricing 
            mechanism and the gradual increase in commercial aviation traffic at 
            Seeb International Airport,’’ reveals Al Lawati. Although he admits 
            that Shell Oman did not fully compensate that loss by the year end, 
            ‘‘we did go a long way in balancing it out,’’ he claims. <br>
            <br>
            <b>Outlook 2005</b><br>
            The outlook for 2005 is generally strong, given the encouraging 
            economic scene in the Sultanate. ‘‘The positive specialist 
            appraisals of the country’s economy by agencies such as Standard &amp; 
            Poor’s (country rating improved to BBB, and Rial Omani to BBB+) is 
            expected to have a favourable bearing on Oman’s attractiveness for 
            foreign investments and otherwise,’’ says Dr Wood.<br>
            <br>
            ‘‘Although the regulatory authorities have initiated processes to 
            scrutinise licensing of new retail service stations, the aggressive 
            network expansion in the industry will continue to be a major 
            threat,’’ he says. ‘‘We not only try to express our point-of-view to 
            the regulator— the Ministry of Commerce &amp; Industry (MOCI)—about 
            sustainable development, but also demonstrate our commitment in that 
            regard,’’ adds Al Lawati. <br>
            <br>
            The fact that when, earlier last year, the MOCI revealed that the 
            number of new licence applications for filling-station sites waiting 
            to be considered by it were several times more than the total 
            existing sites in Oman, Shell Oman’s applications among those were 
            just a handful bears testimony to that statement. <br>
            <br>
            The commercial market is expected to continue offering volume growth 
            opportunities, some of which might be available at the cost of 
            increased risk of extended credit. The major construction project 
            activity is also expected to provide increased demand, albeit with 
            the backdrop of increasing levels of price competition.<br>
            <br>
            The major concern felt in the business environment is that of the 
            aggressive retail network growth, exceeding the growth of market 
            volumes, which has the affect of taking business from existing sites 
            in the industry network. ‘‘Unless controlled, this trend will reduce 
            the financial performance of such investments as compared to that 
            projected at their implementation by dealers and marketing 
            companies,’’ says Dr Wood.<br>
            <br>
            Besides, lubricants blending activity in the Gulf region is on the 
            rise. ‘‘Growing competition may expose a significant part of the 
            export volumes, which the company competes for on blending fees and 
            through its high quality standards,’’ says Dr Wood.<br>
            <br>
            <b>Corporate social responsibility</b><br>
            The year 2004 saw intense activity on Sustainable Development and 
            social investment related initiatives. ‘‘The company’s strong belief 
            in Sustainable Development underpins the vigilance it adopts across 
            all the four aspects of HSSE, and we will maintain our responsible 
            and proactive approach to these vital issues,’’ says Dr Wood.<br>
            <br>
            Last year, in addition to the traditional involvement in the Muscat 
            Festival and Khareef Festival, Shell Oman partnered with the tourism 
            authorities to launch the successful “My Beautiful Oman” campaign, 
            targeting the promotion of domestic tourism in Oman besides 
            following a busy agenda of charitable sponsorships and activities.<br>
            <br>
            Shell Oman is also trying to add to the country’s Omanisation 
            quotient through its Select chain of convenience retail stores. 
            ‘‘Our corporate philosophy is to engage, participate and partner the 
            regulators in whichever market we are. In this case, it means 
            finding opportunities for Omanis. The one initiative that we 
            recently embarked on was to develop and train Omanis to take over 
            the management—and not just work as salesmen—of Select stores. We 
            have a number of stores already in the network that are being run by 
            Omanis as their own businesses, says Al Lawati.<br>
