            <font face="Verdana" style="font-size: 11pt">National Bank of Oman</font></span><font face="Verdana" size="4"><br>
            </font>
            <span style="font-weight: 700">
            <font face="Verdana" style="font-size: 16pt">PAST TENSE</font></span></p>
            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">
            Putting its troubled past behind it, National Bank of Oman 
            concentrated on its rebuilding exercise in 2004. While that resulted 
            in it dropping four slots—from No. 3 last year to No. 7 this 
            year—the bank indeed managed to get back in the black albeit not 
            without its share of bad press in terms of a failed merger attempt 
            with the No. 2 ranked BankMuscat</font></b></p>
            <p align="justify" dir="ltr">
            <b>
            <img border="0" src="b_v.jpg" align="left" vspace="2" width="200" height="250"></b></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">W</font><font face="Verdana" size="1">hen 
            OER’s Top 20 was compiled last year, John Finigan, the then-CEO of 
            National Bank of Oman (NBO) had optimistically stated that despite 
            the setbacks it was facing, NBO would generate profits from 2004. 
            Breaking a jinx of three successive years in the red, the bank has 
            delivered on that front, registering net profits of RO5.2 million in 
            2004, despite additional provisions for loan loss – a significant 
            turnaround from the net loss of RO51.7 million declared in 2003. The 
            bank’s operating profits stood at RO16.2 million. The financial 
            results acquire greater significance in the light of the trying 
            phase the bank has been through last year with an announcement of a 
            proposed merger with BankMuscat late last September.<br>
            <br>
            “In terms of balance-sheet evolution, the focus in 2004 was cost 
            control and asset liability management with special emphasis on fund 
            management,” says Sheikh Suhail Bahwan, chairman of NBO. Based on 
            this focus, the bank has reassessed its requirement for high cost 
            deposits and re-priced them, which has resulted in the bank’s 
            deposits coming down to RO542 million last year from the 2003 figure 
            of RO621 million. <br>
            <br>
            “Last year, we concentrated on our non-performing assets (NPA) 
            portfolio. Out of the gross advances, amounts upon which interest 
            was not being accrued or where interest had been reserved totalled 
            RO256 million, and we made a provision of RO209 million against 
            them, which represents an increase in our provision coverage on the 
            NPAs from 73.2 per cent in 2003 to 81.5 per cent in 2004. We have 
            also achieved a capital adequacy ratio of 19 per cent,” states B. 
            Vasanthan, chief executive officer, NBO. The paid up capital of the 
            bank remained status quo at RO70 million whereas the net worth after 
            incorporating all adjustments stood at RO102.46 million as against 
            RO97.2 million in 2003. <br>
            Since most of NBO’s woes stem from its trouble-prone credit 
            operations particularly in the overseas branches in Egypt and Abu 
            Dhabi, last year, the bank adopted a two-pronged strategy of prudent 
            profitable business development and dynamic remedial management to 
            bring the performance back on track. “Credit quality continued to be 
            our overriding concern in 2004. Our Corporate Banking operations 
            were centred on business generation and account management. We have 
            developed target markets for new lending opportunities with business 
            development closely aligned to risk appetite,” reveals Vasanthan.
            <br>
            In a conscientious effort to mitigate the fiasco in the overseas 
            operations, NBO focused on improving corporate lending in a 
            selective manner, with emphasis on credit quality in its single 
            branch in Abu Dhabi. The consumer lending criteria were further 
            tightened according to officials at the bank. NBO is hopeful of 
            breaking even in Egypt this year. “Our issues have been resolved and 
            our strategy for the five branches in Egypt continues to be on a 
            ‘care and maintain’ basis with emphasis on recovery and settlement 
            of the NPAs, deposit generation and expense reduction,” claims 
            Vasanthan. <br>
            With regard to Remedial Management, NBO continued to pursue all 
            major recalcitrant debtors relentlessly and also worked on 
            rehabilitating stressed accounts, which were in a relatively early 
            stage of impairment. The strategy paid off with NBO registering a 
            record amount of collections totalling RO27.3 million.<br>
            As a part of its customer-centric approach, the bank has evolved 
            value-added services including shifting certain branches to better 
            premises, introducing evening banking at the Qurum Branch in Muscat 
            as well as installing new ATMs in Suwaihrah (Batinah), Dhank and 
            Salalah. <br>
            The Consumer Banking Division also launched the SMS Banking 
            service—Al Mersal—during the year and devised special loan packages 
            based on segmented information. Key initiatives in Information 
            Technology (IT) include the upgrade of the Core Banking System to 
            the latest release, enhancement of the SWIFT interface as mandated 
            by them, completion of IT efficacy study, standardisation of 
            desktops for improved security and successful testing of the 
            disaster recovery plan.<br>
            <br>
            Merger called off<br>
            The proposed NBO-BankMuscat merger, which was irrefutably the 
            biggest corporate story of 2004, became an anticlimax when it fell 
            flat early this year. “In February this year, when the Board of 
            Directors met to consider the various issues and outstanding matters 
            relating to the proposed merger, and after considering the best 
            interest of all the stakeholders and the ongoing delays in reaching 
            an agreement—the merger was to have been effected on 1st January 
            2005—it was decided not to pursue with the proposed merger,” 
            explains Vasanthan.<br>
            The chairman also has made it clear that the bank is at present not 
            considering a merger with anyone else, and will continue functioning 
            as an independent entity. His recent statement that the decision of 
            his group—Suhail Bahwan Group Holdings—to invest in NBO was right 
            and that they would continue supporting the bank as a major investor 
            has also laid to rest all speculation regarding the sale of his 
            group’s 34.5 per cent stake in the bank to UAE’s Shuaa Capital. In 
            Sheikh Suhail Bahwan’s words: ‘‘We are convinced that this bank has 
            a strong infrastructure that enables it to go ahead and achieve 
            great success to serve the domestic economy.’’<br>
            <br>
            Staff and training<br>
            Actively pursuing its localisation policy, NBO has upped its 
            Omanisation percentage to 93.39 per cent, and Omanis, including 
            women, occupy the top slot a all its branches. “We are working 
            actively towards raising the efficiency levels, and with this in 
            view, our Training Department continues to offer top quality banking 
            courses to our employees. We also send our staff for external 
            training if required,” states Vasanthan.<br>
            <br>
            Future perfect<br>
            Given the continuing resilience and buoyancy in the domestic market 
            and regional economies, the bank plans to target the oil and gas 
            sector as well as government and other quality assets in Oman and 
            UAE to increase the overall loans assets. The focus, to a major 
            extent, will remain on improving the overall asset quality in the 
            remedial portfolio.<br>
            Streamlining its operations to become a stronger bank is another 
            crucial aspect of the agenda. The bank intends to make its credit 
            and operations, internal control, risk management, advisory, 
            products and services much stronger. Officials portend this strategy 
            will result in an improved configuration of the bank’s position and 
            give a boost to customer satisfaction and shareholder value. As a 
            move in this direction, NBO proposes to implement a strategic 
            quality customer service assurance system that would be fully 
            operational by end-2005.<br>
            Vasanthan says: ‘‘The customer will remain the focus of our 
            operations, and we will continue to evolve as a customer-centric 
            organisation. We will be launching some exciting new products in the 
            latter half of this year, and this will not only involve a 
            reengineering of our internal processes, but also include improving 
            the quality of our staff through continuous training. We will 
            improve on our last year’s performance and the results will be 
            visible in future.”<br>
