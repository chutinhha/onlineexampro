            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">After 
            suffering losses for three consecutive years, Oman Air clocked 
            impressive financial results in 2004, never mind the fact that it 
            slipped a step in The OER 20 rankings – from No. 4 last year to No. 
            5 this year. Despite the rise in fuel prices, the company has 
            managed to bring its balance sheet back in the black</font></b></p>
