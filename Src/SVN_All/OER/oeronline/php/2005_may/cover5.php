            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">A</font><font face="Verdana" size="1">dversity 
            is the blessing of the new. It couldn’t be truer than in the case of 
            Oman Aviation Services (OAS). Had it not been for the rise in the 
            aviation fuel prices, the RO0.628 million profits posted by 
            OAS—after making losses for three successive years—would have earned 
            more attention.<br>
            <br>
            The airline industry went through a bad phase from 2001 to 2003, 
            starting with the September 11 mishap, followed by the SARS epidemic 
            and the war in Iraq. Luckily, 2004 proved to be a year of growth, 
            which revived the industry. Passenger and cargo volumes grew by 15 
            and 13 per cent, respectively. After more than two years of the 
            lacklustre and slow phase that the airlines industry faced, the jump 
            in the volume of passengers and cargo was a welcome change – and a 
            much-needed one. <br>
            <br>
            The Middle East and Asia witnessed significant growth with the 
            traffic growing by 25 and 21 per cent, respectively. OAS too 
            received the much-needed boost in its sales graph with this 
            increase. The company’s potential to perform well has always been 
            supported by the government. Recently, the government provided a 
            soft loan of RO10 million repayable after five years in 10 equal 
            annual instalments. Instead of relying only on government subsidies, 
            the company is also planning to raise its equity and seek additional 
            capital from shareholders once the company has attained two 
            successive years of profitability.<br>
            <br>
            Another positive trend adopted by Oman Air that benefits and adds to 
            its black bottomline is the change in the basis of accounting 
            unflown revenue from unused tickets. Till December 31, 2003, the 
            company recognised revenue from unused tickets after two years from 
            the date of sale of the ticket. But from January 1, 2004 the company 
            changed this form of accounting from two to one year. This resulted 
            in additional revenue of RO1.1 million in 2004.<br>
            <br>
            <b>Flying high</b><br>
            It was not just the financial results that improved for OAS but the 
            airline also reported exceptional operating performance. The average 
            seat occupancy went up from 66 per cent to 71 per cent. The 
            passenger volume recorded a 6 per cent growth, with the airline 
            carrying 982,541 passengers in the year. Oman Air also performed 
            well in technical dispatch with 99.5 per cent reliability being 
            achieved despite extremely high aircraft utilisation of 13.2 block 
            hours per day; one of the highest in the industry.<br>
            <br>
            There was improvement in the airport services business also for the 
            airline. Handling of flights went up by 11 per cent; cargo 32 per 
            cent and meal catered grew by 22 per cent. Overall, the airline has 
            been able to paint a positive picture in the year 2004. <br>
            <br>
            But Oman Aviation is taking a cautious approach towards growth 
            because of the mounting rise in the cost of fuel, intense pressure 
            on market share and the competition it faces from other major 
            networks including the introduction of low-cost carriers. As the 
            national carrier of Oman, the airline is equipped to take on the 
            challenges and competition it may face by adopting cost-reduction 
            measures as has been demonstrated by the company in the past years.<br>
            <br>
            <b>Human resources</b><br>
            OAS’ personnel costs increased by 17 per cent; it was RO19,746 
            million in 2003, which increased to RO21,432 million in 2004. But at 
            the same time, the number of employees went up from 2,526 in 2003 to 
            only 2,584 in 2004, an increase by 2 per cent. The airline’s 
            Omanisation percentage remained the same as was in 2003 – 76 per 
            cent of the total staff, with many of them occupying highly 
            qualified positions in senior management, crew and commercial staff.
            <br>
            <br>
            OAS spent RO114 million on training and development of its Omani 
            employees, whereas in 2003, the amount spent was RO163 million. The 
            airline aims to recruit and train promising young Omani nationals, 
            one of the airline’s primary objectives. <br>
            <br>
            <b>Looking ahead </b><br>
            With the aim to improve operations and services, OAS has embarked on 
            a number of initiatives. This includes considerable IT plans to 
            optimise and reduce its unit costs, increase revenue and improve the 
            quality of its products. The year 2005 will witness an increase in 
            its fleet with the addition of a B737 800 aircraft, taking up the 
            number of B737 NG aircrafts to six. Along with the addition to its 
            fleet and also the introduction of new routes, the airline is sure 
            to benefit from economies of scale and further lower its unit costs.
            <br>
            <br>
            Innovative services and products and prudent cost control measures 
            would be two factors that will play important roles in turning the 
            wheels of fortune for OAS.<br>
