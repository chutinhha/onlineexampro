Internal matters</font><font face="Verdana" size="1"><br>
<br>
<b>OER columnist Afshan Patel met with Ashok Kumar Aggarwal, a Director-at-large 
with the Institute of Internal Auditors, to catch up on his views about the 
profession. Excerpts from an exclusive interview</b><br>
<br>
<img border="0" src="main2a.jpg" align="left" hspace="4" width="143" height="220">The 
Institute of Internal Auditors (IIA) was established in 1941 and has its 
headquarters in Florida. The Institute is recognised world over as the ‘global 
voice and advocate’ for the internal audit profession. IIA conducts the 
Institute’s certifying examinations that lead to the award of the designation 
‘Certified Internal Auditor’. <br>
<br>
The IIA has set International Standards for the Professional Practice of 
Internal Auditing. It undertakes valuable research through the IIA Research 
Foundation. Oman Air sponsors IIA Oman, and its activities started in June 2003. 
IIA has recently crossed the 100,000-mark in membership and IIA Oman also has 
achieved an impressive membership of 256 members to date. <br>
<br>
The Internal Audit Department of Oman Air has been the exam centre for the 
Certified Internal Auditor (CIA) exams held in Oman since 2000. IIA Oman is 
professionally very active and organises many professional seminars with 
visiting speakers from overseas and speakers from Oman. Recently, Ashok Kumar 
Aggarwal, a Director-at-large with IIA Inc., was in Oman and Afshan Patel, 
member of the Education Committee of IIA Oman, caught up with him. This is what 
he had to say:<br>
<br>
<b>What brings you to Muscat?</b><br>
I am a Director-at–large on the Board of the Institute of Internal Auditors, 
Inc. USA, and I am also on the International Relations Committee. By virtue of 
that, I have been tasked with reviewing the Oman Chapter-in-formation and, 
therefore, the purpose of my visit is to ensure and give feedback to the 
committee that the Chapter-in-formation is conducting itself in the desired 
manner and in line with the objectives, that there is a sound base established 
with a good Board of Directors and that they have set goals and objectives in 
common with IIA Inc. and that they are providing value for money services to 
their members. <br>
I was impressed that IIA Oman is reaching out to captains of industry and the 
government to ensure that governance issues are properly projected. The visit 
also seeks to establish if there is need for any mentoring, counselling or 
direction. Subsequent to my visit, I will make a report to IIA Inc. and 
recommend that the Oman Chapter-in-formation be given the status of a Chapter.<br>
<br>
<b>Are you undertaking any similar assignments for any other country?</b><br>
I happened to be in Doha on a similar mission about two months back. Last year, 
I helped the Bangladesh Chapter to be set up. I am also trying to explore 
setting up a chapter in Sri Lanka, which is trying to get the requisite numbers. 
Bangladesh, on the other hand, already has a full-fledged Chapter now with a 
membership of about 808 within a year.<br>
<br>
What is your perspective on the degree of regulation like Sarbanes Oxley that 
has come in the wake of the international corporate scandals? Do you feel that 
it is an overreaction?<br>
Well yes and no. There are certain factions that think that there has been a 
knee-jerk reaction and that probably the regulators in the US were pushing too 
much, too far. But I think now that things have stabilised, corporates have seen 
the value in what Sarbanes Oxley demands. However, there is a feeling that some 
of the measures are too harsh; especially the accountability on the part of the 
CEO and CFO is quite acute, but I suspect the dust is settling down and the 
managements and the Boards will realise the value of Sarbanes Oxley.<br>
<br>
<b>Do you feel that greater regulation and compliance requirements hamper the 
creativity of management in developing growth strategies and pursuing further 
business avenues?</b><br>
Overall, I think businesses will grow – they have to grow. While managements 
will have to be given sufficient latitude to operate, they will have to learn to 
operate within the legal and regulatory framework and there are no shortcuts to 
that. The harsh reality is that regulation will only increase. In the US now, we 
have the Anti-Money Laundering Act, there are laws to combat the financing of 
terrorism and there is the Patriot Act. All this is only seeking to ensure that 
managements comply and establish high standards and best practices. <br>
<br>
<b>What has been India’s response to the corporate scams? Did India also respond 
with increased regulation?</b><br>
Yes, India also responded (in a similar manner). There was the Kumaramangalam 
Birla Committee set up to look into the aspect of corporate governance and it 
submitted its report, which was accepted by and large. Today, both in the 
Company Law and stock exchange listing requirements, fairly onerous provisions 
have been brought in particularly to ensure that audit committees are in place 
with, as far as possible, independent directors who have an oversight role of 
ensuring that governance processes—particularly risk management and internal 
controls—are in place. <br>
<br>
Further, the Board of Directors are required to publish in the annual report 
separate paragraphs on risk management and internal controls and insofar as 
audit committees are concerned, they are required to publish the names of the 
members, their qualifications and the number of times the audit committees met 
during the year. <br>
<br>
<b>How do you think the level of ethics among the holders of top offices can be 
improved?</b><br>
Ethics really relates to intellectual honesty. If management is serious about 
induction of ethics and ensuring that ethics are widely prevalent across the 
organisation, there has to be an ethical culture and a host of parameters come 
into play in any professional organisation. Ethics really has to be a tone set 
from the top.<br>
<br>
<b>If you ask the CEOs, a majority of them wouldn’t have nice things to say 
about their internal audit departments. What do you think can be done to improve 
the image of internal auditors from the perspective of the management?</b><br>
My experience where CEOs are concerned—and I have sat on many platforms where 
there are CEOs and internal auditors across the table—I think the CEOs clearly 
recognise the contribution and the role of internal auditors. In fact, many a 
CEO has himself gone through the route of internal audit at some time or the 
other; so, my experience is that CEOs and managing directors of large 
organisations do appreciate the role of internal audit per se. <br>
<br>
If a CEO who is intellectually honest wants to get value from the internal 
auditing profession, he will jolly well ensure that there is a meaningful audit 
set-up in place. He will not wait for legislation to impose internal audit on 
him. So, my experience runs otherwise. But I am aware of situations where there 
are CEOs who are obviously not too happy with the auditors because they could 
throw up things that could be embarrassing at times. Not necessarily 
mismanagement, but sometimes to do with decision-making where some element of 
loss or opportunity cost gets incurred as a result of faulty decision-making.<br>
<br>
Today, the role of the internal audit is well recognised because in any 
organisation, there is tremendous scope for improvement and if you have 
fit-for-purpose auditors, it is here that they will lend an independent view and 
contribute to the organisation.<br>
<br>
<b>What in your opinion are the greatest challenges facing the profession 
globally and in the region?</b><br>
The immediate challenge internal auditing is confronted with is to get 
recognition. In many countries, qualified internal auditors being few and far 
between, there isn’t a sufficiently large base to push for recognition. The 
small numbers is also a constraint for the legislators and regulators to enact 
laws that all listed companies at least must have a qualified internal auditor.
<br>
<br>
A further challenge of recognition is for the regulators, legislators and 
management to recognise internal auditing as a major contributor to risk 
management and internal controls in any organisation. In the current situation 
and particularly in the context of scams, I think the role that internal audit 
can play is being recognised and it is just a question of time and, one day, 
regulators will have to sit up and pass some regulation like in the case of 
Company Secretaries in India where all companies must have a qualified company 
secretary. The auditing profession also waits for the day when it will become 
mandatory for listed companies to have a qualified internal auditor.<br>
<br>
<b>What, as office bearers of IIA Oman can we do to make it a really active 
branch and an important part of IIA?</b><br>
One important aspect that IIA Oman is already working on is to get recognition 
as a legal entity. What I have seen of my meeting with the board is that they 
mean business. They have a number of auditors representing the who’s who of the 
Omani industry and a membership base of 256 is also very impressive.<br>
<br>
<b>What responsibilities does being a Director-at-large on the Board of IIA Inc, 
USA, entail and what have been your contributions to this office?</b><br>
In terms of areas of responsibility, there’s no specific responsibility and 
that’s one of the reasons why the designation is Director-at-large vis-à-vis a 
director having a specific responsibility. Nevertheless, I must confess that I 
have to understand the definition of ‘at-large’. Within the short period, I have 
visited a few countries trying to establish their chapters. But what I think is 
important is to make sincere effort in promoting the profession. <br>
<br>
IIA is recognised as an authority and acknowledged leader and chief educator for 
the profession worldwide. Therefore, I try to work to give it a strong voice and 
visibility with regulators and legislators and ensure that programs that IIA 
India run are value for money to the members and I work towards increasing the 
membership base.<br>
<br>
As a member of the International Relations Committee, we work towards the 
globalisation of the profession. Till a couple of years ago, it had 
predominantly North American members and those from some European countries, but 
now it is reaching out to a far greater number of countries. The publications 
are being translated into a number of regional languages and to that extent we 
are trying to make the profession as global as possible. N</font></p>

