            <span style="font-weight: 700">
            <font face="Verdana" style="font-size: 16pt">A NEW BEGINNING</font></span></p>
            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">The 
            year 2004 proved to be the most challenging year for Oman Oil 
            Marketing Company, given the need to transform all its sites from BP 
            Oman to Omanoil while maintaining its performance and customer base. 
            The company met these challenges commendably, and climbed a notch in 
            The OER 20 rankings – from No. 5 last year to No. 4 this time around</font></b></p>
            <p align="justify" dir="ltr">
            <img border="0" src="mike.jpg" align="left" vspace="2" width="200" height="250"></p>
            <p align="justify" dir="ltr">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">L</font><font face="Verdana" size="1">ast 
            year will be recorded as another successful year for Oman Oil 
            Marketing Company (OOMCO) in more ways than one – the company has 
            continued to grow its profit before tax despite increasing 
            competition and has totally rebranded the Filling Stations, 
            Commercial &amp; Aviation businesses, a challenge that started in 2002 
            and was successfully concluded in the early part of 2004.<br>
            <br>
            Drawing upon its core competencies of operational efficiencies and 
            flexibility, OOMCO has commendably braved the challenge posed by the 
            need to transform its corporate identity from BP Oman to Omanoil 
            without negative impacts and in the sixth year of its operation as a 
            public company, has showed yet another fine performance.<br>
            <br>
            Increased competition not withstanding, OOMCO has managed to achieve 
            a creditable increase in both volume as well as turnover exceeding 
            2003 by 15.9 and 20.7 per cent, respectively. These increases, 
            especially in Aviation, where pricing is linked to global crude 
            prices, have jointly propelled the company’s turnover forward to 
            RO70.78 million in 2004 (2003: RO58.66 million), marking an increase 
            of RO12.1 million. That this performance has been possible when 
            counterpart Shell Oman Marketing Company’s revenues have dipped from 
            RO160.2 million in 2003 to RO139.7 million in 2004 speaks about the 
            resilience of the company and its ability to weather challenges 
            owing to a firm focus on its fundamentals.<br>
            <br>
            This, combined with cost containment, helped OOMCO realise a before 
            tax performance exceeding 2003 where, despite a significant increase 
            in depreciation charges arising out of the re-branding of the 
            company, the profit before tax (PBT) for 2004 is RO2.6 million 
            compared to RO2.2 million for 2003. However, despite the higher PBT 
            in 2004, the profit after tax (PAT) was 12.6 per cent lower at RO2.3 
            million due to the ‘‘one off’’ credits in 2003 on account of write 
            back of tax provision of earlier years and deferred tax.<br>
            <br>
            The balance sheet remains strong with continued focus on debtors and 
            cash management. As a result of aviation sales (secured) and dues 
            from government companies, the debtors have increased by RO2.7 
            million. In a repeat of what Mike Wilson, managing director, OOMCO 
            had told OER in 2004, “The continuing focus on business improvements 
            and new opportunities has led to aggressive capital expenditure,” 
            OOMCO’s capital expenditure totalled RO3.1 million, funded through 
            internal accruals, thereby increasing the asset base. This has 
            resulted in an increase in the net asset value per share to RO1.99 
            from RO1.89 in the previous year.<br>
            <br>
            In line with its creditable performance in 2004, the company has 
            recommended a cash dividend to shareholders of 300 baisas per share 
            that represents a payout of 30 per cent (2003: 26 per cent) of the 
            ordinary paid-up share capital.<br>
            <br>
            <b>Going places</b><br>
            “The environment in 2004 was both challenging and deeply rewarding 
            given that we, as a company, completed the re-imaging of the 
            remaining 54 retail sites, our vehicles and all buildings in the 
            early part of 2004. This was done in record time and within budget,” 
            remarks Salim Abdullah Al Rawas, chairman, OOMCO. <br>
            <br>
            Though the aviation business failed to meet what was an ambitious 
            target to begin with, it did manage to produce results that were 
            credible, says the chairman. The change in aviation pricing 
            instituted after representation from all marketing companies now 
            places Oman on a comparative basis with all other airports in the 
            region and early indications are that this may see an upturn in 
            volumes but at slightly reduced margins.<br>
            <br>
            Last year, the fuel retail sector was spurred on by a general growth 
            in the sector as well as the higher levels of construction and other 
            project activity in the country – this, in turn, has triggered an 
            aggressive approach to retail network expansion in the industry. As 
            promised last year by Wilson in an interview with OER, “In 2004, our 
            company is set to build more sites than we built last year. 
            Ultimately, our retail performance will be dependent upon the level 
            of customer service we offer, our brand image and the control of our 
            cost base,” OOMCO’s retail sector was the most active it has been in 
            many years with 5 new sites being commissioned and 5 rebuilt.<br>
            <br>
            He added that the decision by the Ministry of Commerce and Industry 
            to address the whole process around the granting of permits given 
            that there is a historical imbalance in allocations was applaudable 
            as this would have the most impact on the company’s ability to 
            develop strategic sites now and into the future.<br>
            <br>
            <b>Opportunities beckon</b><br>
            As part of BP earlier, BP Oman was restricted in terms of 
            geographical expansion outside of Oman so that it didn’t conflict 
            with any of BP’s other businesses. Now, the exit of BP has opened a 
            floodgate of opportunities to the company and it certainly has lost 
            no time to press on this advantage. As a part of its strategy to 
            expand core business revenue streams outside of Oman more 
            vigorously, OOMCO entered into a joint venture agreement with Al 
            Sarooj Group LLC in June last year. <br>
            <br>
            Under the terms of the Agreement, the company has a 50 per cent 
            interest in a jointly controlled entity—Oman Oil Marketing &amp; Sarooj 
            Group LLC—a limited liability company incorporated in the Sultanate 
            of Oman with share capital of RO40,000. The joint venture’s 
            principal activity will be to carry out commercial activities in the 
            oil and gas sector outside the Sultanate of Oman.<br>
            <br>
            <b>People</b><br>
            The chairman credits the company’s good results to its staff, 
            saying: “The staff commitment and level of performance was 
            responsible for the fine performance in 2004. Even more encouraging 
            was that this was done against a background of changing processes, 
            procedures and image; at all times they showed a high level of 
            professionalism.”<br>
            <br>
            In line with the national initiative to develop young local talent 
            in the country, OOMCO has embarked on a programme that, over three 
            years, will take the localisation levels to 90 per cent from its 
            present 74 per cent. This will be done in such a manner that it will 
            ensure a smooth transfer of skills while maintaining high levels of 
            performance according to company officials.<br>
            <br>
            <b>Way forward</b><br>
            “A lot of work has taken place in identifying suitable investment 
            opportunities in the region, and our partnership with the Sarooj 
            Group with plans to expand into the region has enabled us to 
            establish sound contacts in a number of locations. This area of our 
            business, where it is believed significant gains will be seen in the 
            future, will be receiving greater attention in 2005,” says the 
            chairman.<br>
            <br>
            The company has also reached formal agreements with BP in both the 
            Lubricants and Aviation businesses to continue marketing their 
            lubricants and receive technical, commercial and marketing 
            assistance for the Aviation business.<br>
