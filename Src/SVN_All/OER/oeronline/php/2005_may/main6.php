market continued with its bull run during the period under review, rising 
further to newer heights. From around the 4200-4300 band, the market slipped 
during the initial fortnight, reaching a nadir of 4175 before picking up in the 
second half of the fortnight. Overall, in a span of 11 trading sessions ended 
April 21, 2005, the market gained 430 points to end at 4600 levels, a level last 
seen during the early 1998 period.<br>
<br>
The largest contributor continued to be the Industry sector, which again gained 
by 13.3 per cent during the period under review to close at a new 52-week high 
of 3850 levels. Overall, Industry stocks have gained a phenomenal 40 per cent 
during the current year itself. Following the industry sector has been the 
banking sector, which also came a close second with a 10.2 per cent gain during 
the period under review. Overall, the banking &amp; in<img border="0" src="index.jpg" align="right" vspace="3" hspace="4" width="250" height="111">vestment 
sector has been the largest gainer on back of huge upsides in the main banking 
stocks due to re-rating which has again percolated down to the investment 
companies, which largely hold these stocks.<br>
<br>
The important factor underlying the bullish sentiments can be gauged from the 
fact that, in spite of majority of the stock getting adjusted to their annual 
dividends during the period under review, the overall markets still gained 
momentum. The gainers largely represented all the three market viz. Regular, 
Parallel and the Third market companies. In fact, the largest gainer was Majan 
Glass, which declared a maiden net profit for 2004 after many years of suffering 
losses. Other front-runners included Oman Cables, which continued to 
consistently improve its profitability along with the cement companies, which 
continued to improve further due to huge domestic demand. <br>
<br>
Other stocks which also shared the limelight included United Finance which 
continued to be locked on a upper circuit on the announcement of its gaining a 
windfall as part of its book gains due to its selling its non-performing assets 
to its group company. <br>
<br>
Topaz Energy and Marine also revived during the latter part of the period under 
review as the company crossed the RO2 to RO2.200-band by gaining 340 baisas to 
touch RO2.340, a new 52-week high for the stock. Other stocks that improved 
included Al Anwar Ceramics on better corporate performance, Oman Chlorine, ONIC 
et al. Overall, around 49 companies gained during the period as against just 41 
companies which lost out. If one compares the market breadth over the last six 
months, we find a very good correlation between the MSM Index and the advances 
and decline ratio. However, if one looks at the trend in the advances and 
decline ratio, it seems that the overall peak has been formed.</font></p>

