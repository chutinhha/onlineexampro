            <span style="font-weight: 700">
            <font face="Verdana" style="font-size: 11pt">Oman Cables Industry</font></span><font face="Verdana" size="4"><br>
            </font>
            <span style="font-weight: 700">
            <font face="Verdana" style="font-size: 16pt">DAWN OF AN ERA</font></span></p>
            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">
            Having leapt from last year’s position at No. 14 to No. 10 in the 
            coveted The OER 20 rankings, Oman Cables Industry is this year’s 
            surprise with a neat 62 per cent increase in its 2004 income. Not 
            only that, its net profits rose by 103 per cent, reflecting the 
            increasing demand for its products in the local as well as regional 
            market</font></b></p>
            <p align="justify" dir="ltr">
            <b>
            <span style="font-size: 11pt">
            <img border="0" src="hussain.jpg" align="left" vspace="2" width="200" height="250"></span></b></p>
            <p align="justify">
            <font face="Verdana" style="font-size: 11pt; font-weight: 700">“T</font><font face="Verdana" size="1">he 
            year 2004 was a landmark year for Oman Cables Industry (OCI) – a 
            very special landmark year,’’ says Hussain bin Salman Al Lawati, 
            vice-chairman and managing director, OCI. Indeed, from being a 
            humble set-up (one that would be considered “cottage industry” in 
            most countries according to Al Lawati) that started two decades ago 
            to becoming one of the giants with the highest turnovers in the 
            country—in the manufacturing sector—OCI is well on its way to 
            achieve its vision of growth and reliability. <br>
            <br>
            Having leapt from last year’s position at No. 14 to No. 10, OCI is 
            this year’s The OER 20’s surprise with a neat 62 per cent increase 
            in its 2004 income – from RO22.16 million in 2003 to RO36 million 
            last year. If that doesn’t impress you, take a look at the cool 102 
            per cent increase in net profits that the <br>
            company netted during the year – from RO0.68 million in 2003 to 
            RO1.37 million in 2004. It is no wonder, then, that the markets 
            rewarded the company with an increase in its share price from 
            RO3.200 in January 2004 to RO4.660 during December 2004 – a 45.63 
            per cent overall jump during the year.<br>
            <br>
            This could very well be credited to factors such as an increase in 
            raw material costs, increase in production capacity and the addition 
            of the new medium voltage (MV) cables range in 2003. But there’s no 
            way you can leave out the roles that strategic planning, structured 
            growth and able leadership have played in taking OCI to where it is 
            today.<br>
            <br>
            <b>Slow but steady</b><br>
            The story of OCI is one of consistency and tenacious vision. Having 
            seen this result long ago, a company that started 20 years ago with 
            all of 10 people as its employees and a revenue of RO375,000, has 
            today grown into one that has a turnover of RO36 million – and that 
            is a remarkable achievement indeed.<br>
            <br>
            Talk to Al Lawati, and he will tell you the OCI story with the same 
            qualities that are the landmark of this firm – consistency, 
            precision and dignity. “Oman Cables has gone through many phases to 
            where it is today, and is all set to grow more,’’ he says. When the 
            company began as a small manufacturing unit, the one question that 
            was constantly asked was why make it such a big firm when making 
            such a small product. “The answer to that too was consistent; this 
            is not a small company, but the beginning of a very big one,’’ 
            recalls Al Lawati. <br>
            <br>
            July 1987 saw the first production of OCI’s regular cables. After 
            suffering losses for the first couple of years, the company finally 
            broke even around 1989-90. The second stage of growth was in 
            1993-94, when the manufacturing and supply of low-voltage cables 
            began. This took OCI from a being a small firm that made traditional 
            products to one that provided the market with a basket of services. 
            Consolidation also came in the wake of this phase. OCI was not only 
            a manufacturer for the local market, but also began exporting to 
            bigger markets overseas.<br>
            <br>
            <b>Dreams unlimited</b><br>
            The final stage, the present one, is one of golden glory, one of 
            startling rise in profits and, most important, the stage where the 
            OCI’s MV cables were unleashed in the European market. “Our first 
            consignment was sent to the UK where we had been supplying 
            low-voltage cables for a few years,” maintains Al Lawati. This 
            helped the company’s standing and reputation but, more importantly, 
            translated a dream into reality.<br>
            <br>
            The entry into the European market added another feather in its cap 
            – that of recognition from neighbouring countries. In this part of 
            the world, says Al Lawati, trying to enter a market with such 
            products is extremely difficult unless one goes through the battery 
            of approvals and inspections. “In European market, we are now not 
            just a product manufacturer, but an identity – a whole institution,” 
            he asserts.<br>
            <br>
            This achievement is not only in terms of the increased profit and 
            initial investment—the project demanded an investment of more than 
            RO4 million—but also in terms of the challenges such as shortage of 
            land, machinery and other barriers that OCI had to overcome in order 
            to meet the production demand of the MV cables, explains Al Lawati. 
            It was not very long back when such cables used to be imported to 
            Oman.<br>
            <br>
            The substantial growth of last year has led OCI to increase its 
            commercial activities, which is what matters at the end of the day, 
            says Al Lawati. OCI is careful to not look at results in the light 
            of immediacy and short-term benefits. This, of course, matters to 
            the investors, but what matters more to him is the stability and 
            long-term performance of the company.<br>
            <br>
            Over the past year, OCI has invested in a modern, high-tech plant 
            and machinery to produce excellent, defect-free cables supported by 
            OCI’s investment into high-tech laboratories and quality control 
            equipment that guarantees a consistently high-quality product. 
            “Where the normal duration of gaining approval in international 
            markets is anywhere between six to eight months, OCI managed to do 
            the same in a space of two months to export the first shipment of 
            the MV cables,’’ adds a very positive Al Lawati. The reasons for 
            this, he maintains, are uncompromising quality and tenacious efforts 
            of the company as a whole to reach the targets it set for itself.<br>
            <br>
            <b>Growth by design</b><br>
            “At OCI, we believe that success is not a target – it is a journey 
            and it must continue,’’ proclaims Al Lawati. So what are the 2005 
            plans for this journey of success as far as OCI is concerned? OCI’s 
            planning strategy isn’t one that plans for a year or for the next 
            step. Al Lawati informs that each term is planned for three years, 
            and the year 2005 is going to be in keeping with the plan, of course 
            with a little updating. <br>
            <br>
            This year, OCI is looking at expanding existing markets while 
            exploring and venturing into new markets in the international arena. 
            The emphasis will lie on interacting with more international 
            contractors to help build a strong overseas market base, taking 
            advantage of the reputation and success that OCI currently enjoys.<br>
            <br>
            Another area of change this year will be Production. ‘‘This year 
            will also see increased production capacity, wherever required,’’ 
            maintains Al Lawati. ‘‘This is in order to serve more customers 
            spread across the local and international market,’’ he adds. “I 
            can’t say for sure that we will achieve these results in 2005, 
            because planning and execution takes time. But I am sure (that) 
            after 2005, in a couple of years, we will match the targets we have 
            set for ourselves in the beginning of the three-year term,” he 
            claims.<br>
            <br>
            ‘‘Management structures are also set to change in 2005,’’ informs Al 
            Lawati, with the new management structure already being launched. 
            ‘‘This structure allows existing employees to grow some more while 
            allowing new blood to come in and carry on the ideals of the 
            company,’’ he explains. <br>
            <br>
            <b>Project Omanisation</b><br>
            ‘‘Special focus will also be given to young Omani graduates who are 
            serious on building a career in Oman and thus helping the society 
            grow further,’’ says Al Lawati. “While this is the condition at the 
            beginners’ level, we are serious about recruiting Omanis in the 
            middle management cadre as well,” he informs. “Yes, their experience 
            cannot be compared to the non-Omanis, but we have special training 
            programmes for those who require it, and methods to recruit them 
            into the company,’’ he assures.<br>
            <br>
            OCI’s localisation efforts have been remarkable with the company 
            reaching 53 per cent of Omanisation. “We are proud of this and are 
            seriously encouraging the process,’’ says Al Lawati. OCI believes in 
            employees, whether Omani or non-Omani, being a part of the company 
            as a whole. “There isn’t a point where the employee is treated as 
            someone who comes in, works and leaves. He or she must be interested 
            in growth and must be in sync mentally with the company,’’ Al Lawati 
            maintains.<br>
            <br>
            “It is only because of employee participation, under the able 
            leadership of our chairman, that we have reached where we are 
            today,” he adds. With Oman’s growing economy and resultant increase 
            in demand of infrastructure products like cables, OCI is bound to be 
            successful taking into consideration its principles and high 
            standards. Al Lawati sums it up succinctly when he says “We have a 
            saying in OCI – ‘Where there’s a will, there’s win.’ We are quite 
            sure of where we will continue on this path of growth.” All the 
            best, Mr Al Lawati.<br>
