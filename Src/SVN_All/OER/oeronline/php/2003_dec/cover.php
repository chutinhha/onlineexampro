                    </font><font face="Verdana" size="4" color="#FF0000">2003 
            The Year that was</font><p><font face="Verdana" size="1"><b>Vicky 
            Kapur and Nayl D'Souza reflect upon major local, regional and 
            international events that shaped 2003, a year that will probably go 
            down in history as the year that redefined business. With inputs by 
            Shuchita Kapur</b><br>
            <br>
            To say that 'it's that time of the year again' will be a bit of a 
            cliché. But then, it is that time of the year again, isn't it? An 
            old calendar year is coming to an end, and a new one is about to be 
            ushered in. It is time to reflect upon the challenges and 
            achievements of the year going by, to look at the triumphs and the 
            failures, the achievers and the also-rans. 'Tis time for hope, 
            expectation and introspection, for synthesising the lessons from the 
            feats and the fiascos, the deeds and the disappointments, the gains 
            and the gaffes.<br>
            <br>
            And what a year 2003 has been! Geopolitical instability and an 
            unpredictable investment climate were the hallmarks of the year, 
            which was by no means 'just another year'. It was a year that 
            witnessed an epidemic outbreak in one of the most popular tourist 
            destinations in the world, an earthquake in Turkey, another failed 
            WTO summit in Cancun, a blackout in two of the most advanced 
            economies in the world, the roll-out of the last Volkswagen Beetle, 
            grounding of the Concorde, a shuttle disaster, wildfires, floods, 
            famines, air crashes and more. <br>
            <br>
            Closer to home, the region saw a war in Iraq and its aftermath, the 
            first WB/IMF summit in Dubai, the launch of a no-frills airline from 
            Sharjah, a change in the royal hierarchy in Qatar, the appointment 
            of a new Crown Prince of Ras al Khaimah (UAE), the creation and 
            collapse of the US' Mideast Peace Roadmap for resolving the 
            Israeli-Palestinian dispute...<br>
            <br>
            The Sultanate saw some bona fide M&amp;A activity in the banking sector, 
            the signing of a number of constructive contracts in the 
            infrastructure industry, a tourism boost through Oman joining the 
            World Tourism Organisation and visa relaxations besides enhanced 
            marketing push for the Muscat and Salalah festivals, the Majlis Al 
            Shura election, a move towards the knowledge economy in the shape of 
            the opening up of Knowledge Oasis Muscat and two IT colleges, 
            consolidation in terms of the new gold souq, rebranding of one of 
            Oman's largest oil giants, the prospect of a new golf course and 
            much more. <br>
            <br>
            OER looks back at the fading backdrop of the year that was, and 
            attempts to catalogue, analyse, investigate, scrutinise, dissect, 
            evaluate and probe the key events that had or are expected to have a 
            major impact on the region's business and economy in general and 
            that of Oman in particular. </font></p>
