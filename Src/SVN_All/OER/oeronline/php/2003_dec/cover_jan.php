              The Omani vessel is set to transport LNG mainly to Korea in the 
              winter season and to France. The government has already signed a 
              contract to build another two LNG vessels. It plans to use Muscat 
              LNG to transport LNG produced by the third train. Meanwhile, 
              during the year, Petroleum Development Oman (PDO) delivered an 
              uninterrupted supply of gas to the processing trains of Oman LNG.
