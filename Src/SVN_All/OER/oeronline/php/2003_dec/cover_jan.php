              <p><font face="Verdana" size="1"><b>Budget 2003 unveiled </b><br>
              With all the aspects necessary for stimulating economic growth 
              incorporated, Budget 2003 was carefully balanced to sustain the 
              ongoing development process. By basing the plans on an average oil 
              price of US$20 per barrel, Budget 2003 represented a conservative 
              stance on part of the government and raised optimism and 
              confidence among the people. With the average oil prices hovering 
              in the $27-$28 per barrel band during the year, experts claim that 
              despite lower exports due to production restrictions (Oman's oil 
              output was down 8.5 per cent in the first half of the year), 
              revenues could be higher than last year when prices averaged 
              $22.71 a barrel. Not surprisingly, then, Oman's economy registered 
              an impressive growth rate of 4.3 per cent in the first half of the 
              year.<br>
