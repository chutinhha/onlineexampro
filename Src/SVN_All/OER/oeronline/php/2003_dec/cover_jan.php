              <p><font face="Verdana" size="1"><b>GCC Customs Union comes into effect
              </b>
            <br>
              With effect from January 1, 2003, the GCC Customs Union 
            came into effect ahead of schedule. It provides a unified customs 
            tariff of five per cent on goods imported into any of the GCC 
            countries and their subsequent free movement within the region. Over 
            400 items have been included in a unified exemption list to support 
            the import needs of domestic manufacturing industries in the region. 
            Meanwhile, a single currency by 2010 is another aim of the GCC along 
            with plans for a common electricity grid and an interlinked natural 
            gas pipeline for the region. During the transitional period from 
            2003 to 2005, the actual implementation of the Customs Union will be 
            studied with a view to removing noticed impediments and streamlining 
            the procedures and practices, explained Salem Ben Nasser Al Ismaily, 
            executive president of the Omani Centre for Investment and Export 
            Development (OCIPED). The GCC Customs Union has now opened the door 
            for negotiating and concluding a Free Trade Agreement between the 
            GCC and the European Union (EU). A determined and accelerated push 
            towards concluding such as agreement would not only enhance the 
            competitiveness of the GCC products in the markets of the EU, but 
            also significantly stimulate the flow of direct foreign investment 
            in the GCC region,&quot; Al Ismaily told delegates at the World Economic 
            Freedom Network Annual Conference at Bratislava in October. The 
            Greater Arab Free Trade Agreement is being implemented ahead of the 
            original schedule, allowing free trade among member states by the 
            end of 2005. Moreover, &quot;annual tariff reduction, which started in 
            1998, is being pursued so that full trade liberalisation will be 
            achieved by 2005,&quot; he added. </font></p>
