            financial aid to poor nations. <br>
            At the summit, held in the Arab world for the first time, ministers, 
            bankers and economists from 184 countries discussed the importance 
            of resuming trade talks that collapsed last month at the World Trade 
            Organisation's ministerial meeting in Cancun, Mexico. <br>
            They also discussed new rules for the currency market and heard 
            pleas from countries such as Afghanistan for greater international 
            assistance. Representatives from another war-torn country, Iraq, 
            also were in Dubai to discuss with the IMF and World Bank possible 
            reconstruction assistance. </font></p>
