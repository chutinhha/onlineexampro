            <p align="left"><font face="Verdana" size="1"><b>Cancun summit ends 
            in deadlock <br>
            </b>The World Trade Organisation (WTO) meeting held in Cancun, 
            Mexico, with the aim of liberalising trade between rich and poor 
            countries ended without any agreement or deals. Most WTO meets often 
            fail as the rich and poor countries do not agree with each others 
            view point or agenda. This time, the developed and developing 
            nations could not agree on many important issues, the most important 
            being agriculture. <br>
            The developing nations accused the rich countries of not opening 
            their markets when they expect the poorer states to do so. The rich 
            countries also did not agree to stop all export subsidies as 
            demanded by the poor countries. This makes the agricultural products 
            of rich nations cheaper in world markets. <br>
            The European Union also drew flak for its proposals for rules to 
            govern investment by multinational companies in the developing 
            world. Both the sides have been blamed for the failure of talks, but 
            the deadlock proved that unlike the earlier times, the developing 
            nations now have real power in the WTO. <br>
            Throughout, the developing countries stood the pressure from the 
            rich nations and were able to maintain a common bargaining position. 
            According to World Bank estimates, around 144 million people would 
            have been lifted out of poverty if the talks hadn't failed in 
            Cancun. Hopefully, the next ministerial meeting, scheduled to be 
            held in Hong Kong in 2004, will see a compromise being reached.
            </font></p>
