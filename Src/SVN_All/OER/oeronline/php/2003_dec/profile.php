                    <br>
                    <br>
                    <b>Nayl D'Souza meets with Dr Hussam Al Rawahy, Principal 
                    Consultant and Managing Director of HMR Consultants, who 
                    reflects upon his career, the successful growth of his 
                    business and Oman's journey towards a sustainable future</b></font></p>
                    <p><font face="Verdana" size="1">
                    <img border="0" src="person-2.jpg" width="200" height="228" align="left" hspace="6" vspace="6">It's 
                    very satisfying to see someone who is helping to move Oman 
                    towards a sustainable future do well in business. That's not 
                    to say that environmentalists and corporate philanthropists 
                    rarely become successful. There just seems to be few making 
                    a name for themselves - particularly in Oman. It's also 
                    refreshing to meet someone whose livelihood is undoubtedly 
                    derived exclusively from a knowledge-based practice which is 
                    other than IT or the conventional doctor and lawyer 
                    professions. Dr Hussam Al Rawahy, 47, is one such person. 
                    Dynamic, charismatic, and articulate, he also had the vision 
                    to realise the potential growing demand for qualified 
                    services in environmental statutes and compliance codes to 
                    support development projects. <br>
                    In 1995, Hussam established HMR Consultants although his 
                    initial plans to form the company began two years earlier 
                    while he was working for Petroleum Development Oman (PDO). 
                    On reflection, he says: &quot;More than anything, I have always 
                    maintained that critical success factors for private sector 
                    ventures are that (a) you are truly passionate about what 
                    you want to do; (b) you actually possess expertise, 
                    knowledge and thorough understanding of the subject; and (c) 
                    that you can recognise or create a market niche for those 
                    services&quot;. His professional knowledge and experience twinned 
                    with his drive and determination to succeed has not failed 
                    him. Today, HMR Consultants is one of the Sultanate's 
                    leading environmental engineering consultancies with a 
                    world-class reputation; attested by their recent milestone 
                    achievement of ISO 9001:2000 certification in recognition of 
                    the quality management system in place at the company. <br>
                    &quot;Oman is very serious about environmental legislation and 
                    its laws are generally based on national as well as 
                    international indices, research and legal statute&quot; he 
                    remarks, reminding readers that the Sultanate is a signatory 
                    to the Kyoto protocol and other international accords. &quot;With 
                    such rapid development in the country during the Renaissance 
                    comes the recognition to ensure that environmental impacts 
                    are minimised. HMR Consultants ensures its clients comply 
                    with regulations and that best practices are adopted to 
                    mitigate negative impacts to Oman's unique and pristine 
                    habitats and natural resources.&quot; <br>
                    Although Hussam concedes that he could have moved into a 
                    potentially more lucrative business, he maintains a 
                    conviction that commercial success can be attained by 
                    combining knowledge and a genuine passion for making a 
                    difference in society. HMR Consultants currently employs a 
                    team of 23 expert specialists to conduct Environmental 
                    Impact Assessments (EIAs), which have become a statutory 
                    requirement in Oman for all developments that may have a 
                    detrimental affect on the environment, as well as to offer 
                    comprehensive water and wastewater resources management, air 
                    quality monitoring, waste management and pollution control 
                    and remediation services. Among other services, the company 
                    also provides environmental auditing, assistance with ISO 
                    14001 certification and delivers environmental training 
                    programmes to all levels of staff. HMR's client portfolio 
                    includes public and private sector entities as well as 
                    international banking and investment groups. <br>
                    <br>
                    <b>Making progress</b><br>
                    HMR Consultants has achieved good growth in recent years and 
                    earned a solid reputation abroad for its work. &quot;New 
                    international players that have acquired oil concessions in 
                    Oman have accumulated baggage from past experiences in other 
                    countries. They are, therefore, particularly sensitive about 
                    environmental matters and it has been our job to address 
                    their concerns,&quot; he says. Hussam reveals that the firm has 
                    been making a number of 'step changes' to ensure financial 
                    buoyancy and sustainability of the firm and to exploit new 
                    opportunities. &quot;Oman is very strategically located and, 
                    without giving away company secrets, I can confirm that we 
                    have plans to tap markets within the GCC, parts of Asia, and 
                    North Africa.&quot; He explains that it has always been important 
                    to stay abreast of the latest technical and regulatory 
                    developments in environmental compliance and to apply this 
                    specialised knowledge towards formulating effective 
                    solutions for his clients' particular challenges. He also 
                    mentions that it's equally important to keep your finger on 
                    the pulse of what will happen in the future. &quot;The 
                    environmental market is constantly changing. The challenge 
                    is being three steps ahead--not necessarily of your 
                    competitors but of the upcoming environmental legislation 
                    and the impact they will have for existing and new investors 
                    and operators.&quot; <br>
                    However, the reality is that HMR Consultants is competing in 
                    a knowledge-based sector, which possesses its own &quot;glass 
                    ceiling&quot; challenges. This includes changing the mind set 
                    that Omani knowledge-based firms can provide world class 
                    services. This is a challenge that HMR Consultants has 
                    embraced with a passion that is reflected in its in-house 
                    staff profile and international client base. &quot;Another 
                    challenge is to promote an appreciation for professional 
                    services to our client base and prospective customers and 
                    negate the common perception that investment in 
                    environmental compliance or mitigation measures represent 
                    non-bankable expenditures. This perception, however, is 
                    rapidly changing especially as we see more international 
                    consortia participate in development projects in the 
                    region.&quot; As it happens, environmental engineering is a 
                    subject that is highly misunderstood. It's more complex than 
                    simply looking at how natural resources and habitats are 
                    affected by man. &quot;The practice demands the ability to review 
                    activities with a holistic approach to systems, rather than 
                    assessing single point impacts and adopting a myopic view. 
                    One must appreciate the technical, social and economic 
                    components of the project as a whole and be able to quantify 
                    impacts, using accepted industry methods and coefficients in 
                    order to design solutions that are of high technical 
                    integrity, fit for purpose and compliant with both 
                    legislative and client needs.&quot; <br>
                    <br>
                    <b>The other issues </b><br>
                    It would be a mistake to only focus on Hussam's credentials 
                    because there are many facets to his persona. He is larger 
                    than life and clearly both an 'ideas-person' as well as a 
                    'people-person'. He is continually looking at improving the 
                    working environment of his staff and there is certainly a 
                    notion of teamwork in the office. A colleague remarks how 
                    pleasant, easygoing, and it is fun to work with him, saying: 
                    &quot;Hussam is a boss who is very much hands-on and is always 
                    ready to participate with great ideas and hard work.&quot; There 
                    is little doubt that this well-rounded engineer has found 
                    something he enjoys doing.<br>
                    Hussam says of his team: &quot;We have been very fortunate in 
                    being able to build a multinational team of talented 
                    individuals who can work together in a multidisciplinary 
                    team or individually to focus on their specialist fields. 
                    And, I am proud to add, that our team does include bright 
                    highly motivated Omani graduates. This has really been the 
                    main factor in enjoying a reputation as a firm that provides 
                    innovative solutions that really do address our clients' 
                    needs and reflect the passion with which we approach all our 
                    work. Our team members all share the same focus, to attain a 
                    high level of customer-care while maintaining technical 
                    integrity and commercial viability.&quot; This legacy is 
                    confirmed by the high level of interest that HMR has 
                    received from Omani graduates and international research and 
                    consultancy institutions who are interested in being part of 
                    the HMR team or participating in joint funded research 
                    projects, workshops and conferences.</font></p>
