            <p align="left"><font face="Verdana" size="1"><b>Georgia: Coup 
            d'etat <br>
            </b>The Opposition took over Georgia's parliament, chasing out 
            President Eduard Shevardnadze and declaring an interim government. 
            Tens of thousands thronged the streets to demand the resignation of 
            the unpopular leader as Shevardnadze, backed by his head of police, 
            declared a state of emergency. <br>
            Just as Shevardnadze began speaking in parliament, protest leader 
            Mikhail Saakashvili and hundreds of his supporters swarmed through 
            the chamber doors, pushing and shoving lawmakers. The protesters 
            knocked over tables and chairs. One leaped up on the speaker's 
            podium, waving a red rose, while another banged the gavel. Later, 
            protesters took over Shevardnadze's office and burned his chair. <br>
            Pro-government lawmakers were thrown out of parliament. The 
            75-year-old Shevardnadze was hustled out of the chamber by 
            bodyguards. &quot;I will not resign,&quot; Shevardnadze vowed outside the 
            building as he boarded a vehicle and was driven off, escorted by 
            troops in riot gear. <br>
            The president later went on national television, surrounded by 
            uniformed officers of the internal security forces, and declared a 
            30-day state of emergency. &quot;Order will be restored and the criminals 
            will be punished,&quot; he said. Opposition leader Nino Burdzhanadze, 
            speaker of the outgoing parliament, proclaimed herself acting 
            president until elections that the Opposition called to take place 
            in 45 days. <br>
            However, Shevardnadze stepped down within a day after the parliament 
            drama amid strong protests against his reign and the disputed 
            November 2 parliamentary poll, expecting no better way out for the 
            crisis-ridden Caucasus nation. Shevardnadze visited his former 
            office in the Georgian State Chancellery two days later, and said 
            that he was leaving the building of the State Chancellery without 
            tears, but with regrets because the building was constructed during 
            his time in government service and he worked in its walls for the 
            better part of 25 years. &quot;I've left here everything I should have, 
            and am taking with me everything I should,&quot; he said. </font></p>
            <p align="left"><font face="Verdana" size="1"><b>UN agency adopts 
            resolution censuring Iran's nuke cover-up </b><br>
            United Nations nuclear monitors have condemned Iran over an 18-year 
            cover-up of its nuclear energy programme and say future violations 
            of non-proliferation obligations will not be tolerated. The UN's 
            Vienna-based International Atomic Energy Agency (IAEA) stopped short 
            of reporting Iran to the Security Council, which could have imposed 
            sanctions. However, arms experts suspect Tehran has more secrets and 
            may face the UN's supreme body in the future.<br>
            The IAEA governing board adopted a resolution in November that 
            &quot;strongly deplores&quot; Iran's 18-year cover-up of a programme including 
            uranium enrichment and plutonium reprocessing - both of which could 
            be pointers to a nuclear arms programme. But the resolution, which 
            was put to the board after a week of haggling between France, 
            Germany and Britain, the resolution's sponsors, and Washington, also 
            &quot;welcomes Iran's offer of active cooperation and openness&quot;. The US 
            sees Iran as part of an international &quot;axis of evil&quot; and believes it 
            has been using a secretive atomic energy programme to screen 
            development of nuclear arms - an accusation Tehran denies.<br>
            IAEA head Mohamed ElBaradei said he was pleased with the resolution, 
            but added: &quot;It is pretty clear that the board is sending a very 
            serious and ominous message that failures in the future will not be 
            tolerated.&quot; <br>
            All 35 members of the IAEA Board of Governors approved the 
            resolution, including the US, Britain, France and Germany, as well 
            as China, Russia, Pakistan, India and Malaysia. </font></p>
