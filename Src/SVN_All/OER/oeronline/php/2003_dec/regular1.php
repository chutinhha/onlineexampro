            <font face="Verdana" size="4" color="#FF0000">Mortgage matters<br>
            </font><font face="Verdana">
            <br>
            </font><font face="Verdana" size="1"><b>The need for an effective 
            quality control in mortgage loan origination has become increasingly 
            important, says Ali Hassan Moosa</b><br>
            <br>
            The importance of residential real estate activities (housing) to 
            the economic health of a country is indisputable as they provide 
            stimulus for economic growth. Construction activities create jobs, 
            activate supply chains (a multiplier effect) and enhance the wealth 
            of the country and its people, not withstanding the socio-political 
            dynamics therein. <br>
            As social demographics along with the economics of the marketplace 
            have changed, so have the dynamics of mortgage banking. It has 
            become a complex financial services business requiring analytical 
            skills and financial modelling and forecasting beyond the levels 
            necessary a few years ago. <br>
            Mortgage banking industry across the globe has shifted its focus 
            from the operational side to the financial side. Mortgage loan 
            production volumes alone are no indication of success if they don't 
            produce a servicing asset with a value commensurate with cost of 
            producing it. To the consumer, long-term mortgage loans are more 
            affordable than other types of loans in terms of lower applicable 
            rates of interest (reflecting lower risk to the lender) and loan 
            repayments. Residential mortgage loans are a sensible way to invest 
            in property ownership. <br>
            Mortgage lending is highly governed by regulation throughout the 
            world. Understandably though, different regulators see things 
            differently. Primary objective of any regulator would be to maximise 
            benefits of mortgage lending to the economy and the public in 
            general. <br>
            In Oman, mortgage-backed lending would classify as 
            real-estate/secured lending but may also fall into different other 
            classifications depending on the type of the issuing financial 
            institution (commercial or specialised banks) as well as licensing 
            and practice codes. <br>
            Recent advances of the many types of mortgage instruments are the 
            result of fundamental changes in the way homes are financed. 
            Mortgage borrowers nowadays have a choice of standard fixed rate 
            mortgage, floating or adjustable (variable) rate mortgage, 
            convertible mortgage (limited period fixed rate) and graduate 
            payment mortgage (flexi payment, starts low and increases over 
            time). <br>
            Of course, there are other types of mortgage instruments such as 
            reverse annuity mortgage which was designed in the US (for social 
            reasons) to help retired homeowners to release the equity in their 
            homes as a supplementary income while retaining ownership. More 
            sophisticated options are also available by some lenders such as 
            &quot;Caps&quot; and &quot;Collars&quot; or &quot;Ceiling&quot; and &quot;Floor&quot; that set upper and 
            lower limits to interest rates in order to protect parties against 
            interest rate volatilities.<br>
            Processing a mortgage loan can be cumbersome. The set of 
            documentation required for a mortgage loan is unique and different 
            than other type of loans. The real estate appraisal (also called 
            valuation) process and report needs to be defined upfront and agreed 
            upon between the lender and the entity (whether in-house or 
            outsourced) that performs the appraisal. Residential mortgage 
            lending is normally considered safe and towards the bottom end of 
            the risk spectrum. <br>
            However, a clear definition of the risk appetite must govern all 
            lending decisions. Loan-to-Value (LTV) ration is one of the most 
            important of all ratios in mortgage lending. The lower the LTV 
            (i.e., more equity contribution by the borrower), the safer the loan 
            is for the lender. It is crucial for any mortgage lender to accept 
            and appreciate a suitable definition of property &quot;value&quot; - is it 
            market value under current circumstances, market value under 
            distress circumstances, cost basis (historical cost for a completed 
            property or projected future cost for a property under 
            construction), or value based on future projected cash-flow (an 
            appraisal method used for investment properties)? <br>
            Mortgage loans are normally amortised over a relatively long period 
            of time - say, 20 years. The repayment will normally consist of 
            principle and interest repayments. In the initial years, there will 
            be lesser principle reduction than in the later years of the loan. 
            The repayment may also consist of insurance premiums that normally 
            cover life and property. Personal insurance cover could be expanded 
            to saving and assurance plans as well as protection against 
            non-payment due to unforeseen events such as loss of 
            employment/income. <br>
            Since mortgage loans are long-term, it is prudent that the lending 
            decision is based on affordability rather than the local practice of 
            salary multiples. This way, the lender acts responsibly towards the 
            borrower as well as reducing the risk of default (should the 
            affordability basis be stringent enough). The source and stability 
            of the borrower's income is of utmost importance, as it will be 
            relied upon to make the mortgage repayments. A good indicator of 
            future income performance is the borrower's past earning history 
            along with the stability of that income. <br>
            The need for an effective quality control in mortgage loan 
            origination has become increasingly important. No longer can a 
            mortgage lender take comfort in falling back on the pledged security 
            in case of default. Lender's duty is to assure shareholders, 
            regulators and other stakeholders that the underwriting process 
            meets not only internally set guidelines but also industry-wide 
            accepted practices and procedures. <br>
            Operationally, it has become essential that mortgage lenders are 
            customer-driven using cross-functional structures and employee 
            empowerment to boost business volumes and enhance customer service 
            leading to higher profitability and shareholder value. The main 
            success ingredient is to increase earnings while making it cheaper 
            to the consumer. <br>
            Key elements in residential mortgage lending are the laws and 
            regulations governing foreclosure. If foreclosure is practically not 
            possible, then the mortgage itself has very little value. The 
            complexity of the law of mortgage banking and the risks it carries 
            for mortgage bankers is critical. <br>
            The judicial proceedings in the Sultanate have evolved over time to 
            reflect social and economic developments where it is possible now to 
            take position of a residential property in case of default. The law 
            is clear and attempts to protect both lenders and borrowers 
            resulting in a relatively long time-frame for mortgage foreclosure 
            execution. It is, therefore, sensible for lenders to establish 
            reasonable foreclosure time parameters within loan loss provision 
            calculations as a technique to mitigate loss.<br>
            Mortgage lenders are exposed to traditional banking risks such as 
            credit, interest rate, liquidity, exchange, solvency, etc. One of 
            the biggest risks to mortgage lenders and borrowers alike is the 
            scenario of negative equity that may occur during the loan term 
            where market value of a mortgaged property may be lower than the 
            outstanding loan balance (say, due to a price collapse in the 
            property market) leading to exposure (a credit risk). <br>
            Other embedded options in mortgages such as prepayments result in 
            shortening the expected life of a servicing asset. Pricing risks and 
            quantifying their true/expected effect under various scenarios of 
            the future is at the heart of risk management, and it requires the 
            highest skills and best analytical methodologies. <br>
            From a financial perspective, one of the most rewarding thoughts to 
            a mortgage lender is the fact that, due to its long-term nature, 
            mortgage portfolio earnings based on Net Present Value calculations 
            are higher than other conventional types of lending. <br>
            In the developed financial markets, the mortgage industry has lead 
            to the development of Mortgage Backed Securities and other financial 
            instruments and derivatives such as Collateralised Mortgage 
            Obligations. The securitisation process is where mortgage loans 
            including collaterals are packaged into interest-bearing securities 
            with marketable investment characteristic used mainly as a funding 
            vehicle. <br>
            Investors in such markets are attracted to engineered securities 
            because of their investment features. From the point of view of the 
            mortgage originator, securitisation may help reduce leverage, 
            improve asset performance and enhance quality, allowing the 
            institution to focus on mortgage underwriting rather than asset 
            management.</font></p>
