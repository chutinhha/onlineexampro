            <br>
            <br>
            <b>The Kia Carnival can easily ferry the average Omani family around 
            town. What more could you possibly want from a car, asks Nayl 
            D'Souza </b><br>
            <br>
            <img border="0" src="car.jpg" align="left" hspace="3" vspace="3" width="200" height="164">Unlike one or two Korean rivals, Kia Motors Corporation has 
            surrendered to criticism that its range of models lacked depth, its 
            level of brand equity was poor, and that its cars failed to make the 
            right impression. Admitting to such flaws and forcing necessary 
            changes to improve what it produces has worked wonders. Although 
            some still regard Kia with a degree of suspicion as an imitator 
            rather than innovator, there is no doubt that it has raised its game 
            and made an extraordinary leap forward as a result.<br>
            Undeniably, in the past few years, Kia has managed truly remarkable 
            growth in the number of dealers who sell its vehicles and its total 
            number of customers. To date, over 10 million Kia vehicles have been 
            sold in over 160 countries and about 200,000 vehicles are sold to 
            the Middle East and Africa each year. Locally, the fact that Oman's 
            Saud Bahwan Group has embraced the brand tells a success story of 
            its own.<br>
            In fact, Kia - the oldest Korean automobile company founded in 1944 
            - has expanded so rapidly that in the last three years, six new 
            vehicles have been added to its model line-up including three 
            heavyweights: the compact, fun and practical Rio; the Sorento 
            midsize SUV; and last, but by no means least, the Sedona minivan 
            which, in the Middle East and other parts of the world, has been 
            repackaged as the Carnival. <br>
            Unfortunately, the MPV segment is a vehicle category that already 
            suffers from a slight confusion in identity. The Carnival, described 
            aptly as a sizeable MPV by Oman's official Kia dealership, is simply 
            a minivan to many in the US or a people carrier to Europeans. No 
            matter which term you use, you get the general gist that this 
            vehicle is roomy. The 2003 model doesn't let you down in terms of 
            occupancy. It is a contemporary urban car that can squeeze nine 
            adult passengers safely. Better still, it can seat seven in plenty 
            of comfort and there is space to stow shopping bags and picnic boxes 
            - the perfect vehicle for the requirements of a typical Omani 
            family. <br>
            OER road-tested the mid-range 2003 Carnival 2.5 GS PWR, two-tone 
            shade, leather-packing model. While the car description is a bit of 
            a mouthful to manage, its price of RO7,010 is far less complicated 
            to mention. Spend a little more money and you can take home the 
            deluxe model, something that is well worth reminding you about later 
            in this review.<br>
            <br>
            <b>Outdoors<br>
            </b>The Carnival's exterior is stylish and simple. It has sleek 
            chrome door handles, body-coloured bumpers, sophisticated lines on 
            its chassis and well-moulded door mirrors. Tinted glass is standard. 
            Entry on this mid-level model is via a keyless entry system.<br>
            <br>
            <b>Indoors<br>
            </b>There are dual sliding doors with child-proof locks for when you 
            want the kids to move in and out of the Carnival safely yet quickly 
            but stay put when you're busy driving. <br>
            The Carnival comes with standard power windows, power mirrors, power 
            door locks, dual air-conditioning, illuminated ignition key slot, 
            and map lights in the overhead console. There are also additional 
            reading lights in the second and third rows of seating, courtesy 
            lights built into the doors and a light in the rear cargo area with 
            a convenient on/off switch. <br>
            The vehicle's 2-2-3 seating layout offers the convenience of a child 
            moving from driver's seat to cargo area without opening a door 
            although I wouldn't condone such a manoeuvre while the car is on the 
            move. <br>
            The driver's seat is an eight-way power adjustable seat with lumbar 
            support. Additional passengers get reclining second-row and 
            third-row seats, which also slide forward and rearward and can be 
            removed. The split-bench configured third row provides extra room 
            for passengers at the rear of the vehicle. In addition to legroom 
            and recline angle adjustments, the middle seats can be rotated 180 
            degrees° to face backwards, allowing convenient conversation with 
            rear seat passengers. <br>
            The vehicle can also double up as a good removals van, especially 
            for shifting long furniture with adequate space for a few occupants 
            for the ride. It is big and wide, but one can't help wishing that 
            the rear seat could be removed completely in a convenient fashion.
            <br>
            As Kia puts it, Carnival's practical layout means there is always 
            plenty of room to move and grow.<br>
            For storage space, Kia has provided oodles of places to keep 
            bric-a-brac, office papers and children's toys. For starters, the 
            car offers two large glove compartments. There is also an overhead 
            console at the front, two places for your sunglasses on the cabin 
            ceiling, an exclusive storage box on the centre console below the 
            gear lever, and a lockable storage bin under the seat. In the rear, 
            there are additional storage areas and seat-back map pockets. In 
            additional to all of this, there's a very handy cubby located on top 
            of the usual map pocket on the inside of the front doors - useful 
            for pens methinks. And if any of the passengers are thirsty, I 
            counted 10 adjustable cup-holders. <br>
            The climate control system allows independent front and rear 
            operation for air-conditioning comfort throughout the cabin. <br>
            The mid-range model OER test-drove has leather on the steering wheel 
            and gear knob. If that's not enough, Kia adds a leather interior as 
            an extra. <br>
            The middle grain finish on the doors is a little disappointing. 
            Although the Carnival boasts a better finish and interior panelling 
            than many of its peers, there are places where the ambience has been 
            tarnished somewhat by the use of artificial wood grain.<br>
            <br>
            <b>Performance <br>
            </b>The Carnival offers a lively performance and has altogether 
            decent road manners. The lengthy wheelbase assures good tracking and 
            despite not having the ability to corner like a sports car, it does 
            provide a good drive for the family on long drives.<br>
            Engine performance is good but nothing to get overly excited about 
            despite having a nifty V6 engine which uses four valves per 
            cylinder. The spread of power at high gear changes could still be 
            more robust. <br>
            The 2.5-litre engine puts out 177 HP at 6,500 rpm and produces a 
            maximum torque of 22.5 kg/m at 4,000 rpm. Although this may not seem 
            much on paper, on the black top this MPV with a heavy load had 
            enough get up and go travelling up the Qurum Heights to surprise 
            some less laden sporty saloons. <br>
            <br>
            <b>Safety <br>
            </b>Driver's airbag, side impact beams, child protection locks, and 
            three-point front seat belts with pretensioners are fitted as 
            standard on the Carnival OER test-drive.<br>
            Second-row passengers are also protected by height-adjustable seat 
            belts with auto-lock retractors, plus a latch system designed for 
            quick installation of child safety seats. The vehicle is designed to 
            meet or exceed all applicable safety standards and has received a 
            five-star rating from the US government.<br>
            For a few hundred Omani rials extra, the top-of-the-range Carnival 
            2.5 GS (priced at RO7,300) can be yours. It comes with dual airbags 
            and anti-lock brake system (ABS) too. That's well worth the extra 
            pennies for peace of mind but more creature comforts are also thrown 
            in such as a CD player, allow wheels, a power sliding sunroof, roof 
            rack and rear spoiler. <br>
            <br>
            <b>The finishing line<br>
            </b>MPVs aren't really my cup of tea personally, but if you were 
            roped into doing the school runs, then it would definitely be a 
            handy helper to have on the driveway. And at the weekends, the 
            Carnival is great to carry the whole family on a break or a picnic 
            outing in the interior. <br>
            Keeping true to the guiding principles of Kia, the Carnival provides 
            a convincing high-quality, high-value vehicle at prices to give the 
            competition a scare. The local dealership assures customers that it 
            will back this up with a good warranty programme (a comprehensive 
            6-year/100,000 km plan) and a customer-first policy.<br>
            But the appeal of the Kia is the price, the base model starts from 
            around RO6,300. For this kind of money, the Carnival is a winner.
            <br>
