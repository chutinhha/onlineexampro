            <img border="0" src="regular1.jpg" align="left" hspace="3" vspace="3" width="125" height="119">Directors of any Omani pubic joint stock company are fiduciaries to 
            the company and its shareholders. As fiduciaries, directors have two 
            paramount duties: the duty of care (see Oman Economic Review, May 
            2003*) and the duty of loyalty. The concept of duty of loyalty has a 
            definite meaning. Simply put, it requires a director to place the 
            company's interests (and those of the shareholders) above his own 
            personal interests in his administration of the company's affairs.<br>
            This duty of loyalty (sometimes termed the &quot;rule of fair dealing&quot;) 
            is often tested in three different types of circumstances. These are 
            when a director (i) has a personal interest in a transaction 
            involving his company (sometimes termed a &quot;related party 
            transaction&quot; or &quot;self-dealing&quot;); (ii) enters into the management of 
            a business which competes with his company; or (iii) uses inside 
            information while purchasing securities in his company (insider 
            trading). These three sets of circumstances are the focus of a 
            substantial part of the recently issued legislation and Capital 
            Market Authority (CMA) guidelines which bring the duty of loyalty 
            into the corporate and legal foreground. This article examines the 
            first and second types of circumstances in which the duty of loyalty 
            is put to the test. Insider trading and the duty of loyalty will be 
            addressed separately in the July issue of Oman Economic Review. <br>
            <br>
            <b>Self-Dealing<br>
            </b>Consider a transaction in which a director proposes to sell 
            property to a company which he serves as a director. In such a 
            transaction, the director would have the duty to maximise the value 
            of the transaction to his company by securing the lowest price for 
            the company. As the owner of the property, the director's own best 
            interest is to receive the highest possible price. The economic 
            motivations of the two positions are in clear conflict. The 
            transaction involves self-dealing: the director has a conflict 
            between his own self-interest and his fiduciary obligations to the 
            company. <br>
            The existence of a conflict of interest does not mean, however, that 
            the completed transaction will be unfair to the company. Contrary to 
            popular belief, having a conflict of interest is not something one 
            is &quot;guilty of&quot;. It is simply a state of affairs. Some self-dealing 
            transactions can be fair and beneficial to the company. For example, 
            an interested director may be in a unique position to help the 
            company because of the knowledge he may have about how the company 
            can benefit from the transaction and the ability to effect the 
            transaction at a minimum cost to the company. The possible 
            beneficial effect of a self-dealing transaction is recognised in 
            Oman law. Specifically, Article 108 of the Oman Commercial Companies 
            Law prohibits any self-dealing transaction unless all the material 
            details of it are fully disclosed to the shareholders and approved 
            by them at an annual general meeting (AGM) before any such 
            transaction is concluded. This legislative accommodation of 
            self-dealing transactions represents an understanding of the 
            potential benefits for the company of such a transaction, and a 
            legal acknowledgement that the company should not be deprived of 
            such benefits simply because it involves a director's conflict of 
            interest. <br>
            Some self-dealing transactions, however, are unfair to the company. 
            For example, a self-dealing transaction will be considered unfair 
            if: (i) the transaction value exceeds its fair market value; (ii) 
            there is a more competitive offer in the market at the time the 
            self-dealing transaction is contemplated; (iii) the company has no 
            clear need to enter into the transaction; (iv) the transaction has 
            no reasonable relation to the objectives of the company (which are 
            stated in the company's articles of association); or (v) the 
            transaction costs compromise the financial condition of the company. 
            Because of the possible dangerous effects of an unfair self-dealing 
            transaction on a company, directors may face civil and, possibly, 
            criminal liability for any failure to observe the requirements of 
            Article 108.<br>
            The shareholders represented at the AGM decide whether or not the 
            self-dealing transaction is unfair to the company and its interests. 
            However, at the request of any shareholder, the company or other 
            interested party, the court may rescind any self-dealing transaction 
            which is shown to be unfair to the company, or is otherwise opposed 
            to its true interests. <br>
            The Code of Corporate Governance, which was issued in April 2003 by 
            the CMA, controls the occurrence of such situations by stipulating 
            that the self-dealing transaction must now be recommended by the 
            company's audit committee before it is presented to the AGM. This 
            provision, therefore, creates an additional check and balance on the 
            directors as a supplement to the control mechanism of Article 108.
            <br>
            <br>
            <b>Managing a Competing Business<br>
            </b>A director's duty of loyalty generally requires that such 
            loyalty be undivided. The old adage that &quot;a man cannot serve two 
            masters&quot; is, therefore, relevant to directors in the exercise of 
            their duty of loyalty, which constrains them from sitting on the 
            boards of two or more companies whose interests compete with each 
            other unless the shareholders give their consent. Article 107 of the 
            Oman Commercial Companies Law provides that &quot;a member of the board 
            of directors may not participate in the management of any business 
            competitive with that of the company, except with the approval of 
            the general meeting, to be renewed annually&quot;. A failure to observe 
            this provision will result in civil, and possibly criminal, 
            liability to the directors.<br>
            Even if the shareholders approve a director's position on the board 
            of directors of the competing company, the court may, at the request 
            of the company, any minority shareholders or other interested party, 
            disregard such approval if it can be shown that a controlling 
            shareholder has a significant interest in the competing company. In 
            such a case, the directors may incur liability for fraud as well as 
            for the violation of Article 107. <br>
            In 2002, and in an effort to reduce the likelihood of such conflicts 
            occurring, Article 95 of the Oman Commercial Companies Law was 
            amended to restrict to three the number of companies an individual 
            could serve as a director (prior to this amendment, the permitted 
            number was five). <br>
            <br>
            <b>Summary <br>
            </b>As a fiduciary of an Omani public joint stock company, a 
            director is subject to the general rule that he shall not, in his 
            dealings with the business entrusted to him, use his relationship 
            with it for his own personal gain. It is his duty to administer the 
            affairs of his company for the common benefit of all the 
            shareholders, and to exercise his best care, skill and judgement in 
            the management of the business solely in the interests of the 
            company. </font></p>
