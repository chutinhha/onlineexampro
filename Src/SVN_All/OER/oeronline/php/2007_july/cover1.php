<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">
‘Continuity is the key’</font></b><font face="Verdana" size="1"><br>
<b>Hussain Bin Salman Al-Lawati, Vice-Chairman and Managing Director of Oman 
Cables, one of the country’s largest non-oil exporters, spells out what is 
making the company progress rapidly on the exports front</b><br>
<br>
<b>
<img border="0" src="cover5.jpg" width="200" height="249" align="left" hspace="6" vspace="4">What’s the secret of your success on the exports front?<br>
</b>We think big. On the last annual staff gathering day, our hon’ble chairman 
stated that while OCI had gained prominence as the largest exporter its aim was 
to increase exports further.<br>
<br>
Machinery alone does not lead to success. We learnt early that OCI cannot 
survive and grow continuously if it is completely dependent on the domestic 
market. Though the demand in Oman is growing, it absorbs only 30 per cent of 
OCI’s currently installed capacity. The fim can easily serve Omani market needs 
for the next decade and more, hence we must look at growth in export markets to 
meet our business goals. <br>
<br>
<b>Are you ignoring demands of the domestic market to concentrate on the 
international market?<br>
</b>OCI’s principle is to serve Oman on a priority basis. The statement that out of 
our total production capacity domestic consumes only 30 per cent is misleading. 
We cover 75 per cent of the domestic market and, as Omani market will grow, we 
will continue to cover atleast 70 per cent of this market at all times.<br>
<br>
Our focus on exports has given us the knowledge and capability to meet various 
cable needs of Oman. OCI always has surplus capacity available to serve domestic 
and other markets to face the unforeseen circumstances. Recently when power 
lines were severely affected due to Gonu cyclone, OCI immediately supplied 
cables worth more than RO1 million to restore power normalcy in Oman by 
diverting products from our export markets.<br>
<br>
<b>In brief, what steps are you taking to increase exports?<br>
</b>Our steps are directed towards building expertise and maintaining dedicated 
commitments at all times. Besides capital for setting up the manufacturing 
facilities, successful international business on a sustainable basis needs 
strategic analysis of the global market to identify opportunities and capitalise 
on them.<br>
<br>
In the last two decades, OCI has been able to gain confidence of its clients in 
international markets such as Japan, South Korea, Australia, UK, Europe, partly 
Africa and the Middle East due to its single-minded commitment, professionalism, 
consistent product quality and good management skills. We are in the process of 
quadrupling our capacity in the coming years.<br>
<br>
<b>How was 2007 for you with regards to the export business?<br>
</b>Upto June 2007, 65 per cent of our sales were from exports and 35 per cent from 
the domestic market. In the first quarter of 2007, we had sales equal to our 
sales for the first six months of 2006. It shows the fast pace at which we are 
growing.<br>
<br>
<b>Where from is the growth coming?<br>
</b>The huge growth of OCI is primarily led by strategic exploration and development 
of new export markets on a sustainable basis. There is a substantial 
infrastructure development taking place all over the world. Our current emphasis 
is on the Europe, North Africa, Arab countries and Australia. We are also very 
active with major EPC contractors, especifically from Japan, Germany, France, 
USA and South Korea. <br>
<br>
<b>What is your plan for expansion of capacity within and outside Oman?</b><br>
Our present expansion of the plant in Oman will be completed by December 2007. 
We believe it is for the society to judge our plans and performance and arrive 
at their own conclusions. OCI started in 1986 on a land measuring 10,000 sq m 
and by 2006, its manufacturing facilities covered over 42,000 sq m, a four-fold 
increase. Recently our management, as part of the long-term plans, acquired 
additional 100,000 sq m and added capacity for medium voltage and high voltage 
cables to meet our export market needs. Our new plant is expected to become 
fully operational
very shortly.<br>
<br>
We have initiated our own PVC manufacturing plant and have plans to introduce 
other vertical integration projects shortly as well and are also looking at 
expanding our capacity in India. To meet international customers’ needs, our 
activities will not be limited to the current product range only. We will be 
offering the full package of products , including special cables, to our 
clients.<br>
