<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Grooming One’s Successors</font></b><font face="Verdana" size="1"><br>
<b>Organisations often fail to plan for their leadership needs when undertaking 
strategic planning. Paul Bridle looks at the effects of this oversight on a 
developing organisation</b><br>
<br>
<img border="0" src="manage1.jpg" width="156" height="365" align="left" hspace="6" vspace="4">Even the best strategy can fail if an organisation doesn’t have the necessary 
leaders with the right capabilities at the right levels. When the time comes to 
implement a strategy, many organisations find themselves struggling to find 
capable leaders to execute the plan. The opportunities are within their reach, 
but they are incapable of achieving them for lack of leadership at every level.
<br>
<br>
Most companies are focused on managing physical resources, budgets, logistics 
and human capital requirements but seldom do they consider leadership needs in 
advance. Few companies recognise the leadership capacity that new strategies 
will require, let alone treat leadership as the starting point of any strategy.
<br>
<br>
What do we mean when we say “leadership”? Leaders are capable of going beyond 
simply managing day-to-day plans or projects. Leaders ensure flexibility and 
adaptability. In other words, leaders optimise performance and sustain 
motivation in the people.<br>
<br>
Managers can deliver results and even some incremental improvements. Leaders 
generate breakthroughs in performance and overcome unforeseen obstacles. A 
recent survey of CEOs revealed that the biggest blockage in companies being able 
to deliver on their strategy is the middle management not being capable enough 
to its abilities as leaders. A company’s leadership reaches well beyond a few 
good people at the top. It typically includes the three to five per cent of 
employees who can deliver breakthroughs in performance.<br>
<br>
Since bold strategies often require breakthroughs on a number of fronts, an 
organisation needs stronger leadership at all levels, if these strategies are to 
succeed. In addition, as the number of strategic dimensions and initiatives 
increase, so does the pressure on the leadership. <br>
<br>
Most CEOs will agree that leadership is important, yet few assess their 
leadership gap precisely. Fewer still build an engine to develop the right 
quantity of leaders with the right mix of capabilities, at the right time to 
match the opportunities that are available. Organisations have often developed 
managers without giving thought to the need for leaders or in the blind belief 
that managers are all they need.<br>
<br>
Even if an organisation has enough leaders, it may discover shortcomings in 
their capabilities. For example, an organisation expanding internationally can 
find that its current leaders lack the cultural sensitivity to operate in 
unfamiliar geographical areas. Similarly, an organisation entering new markets 
can find it has too many engineers and not enough business builders. The failure 
to carefully assess leadership capacity before launching strategic initiatives 
can leave top executives scrambling to fill last-minute gaps. This can result in 
poor choices or promotions that are more of a ‘best of what we have got’ rather 
than what the organisation actually needs to drive the strategy.<br>
<br>
In the short term, companies that undertake new strategies without the right 
leaders in place are forced to burden their existing ones with additional 
responsibilities. In these cases, the leaders get sucked into the demands of 
day-to-day activities and, as a result, strategic planning, people development 
and the ability to take an objective stance are all sacrificed. Although the 
effect of dropping these leadership activities is not immediately apparent, the 
long-term effects are significant.<br>
<br>
If an organisation stretches its existing leaders too far, their overall 
effectiveness takes a nosedive. This trade-off compromises strategic objectives, 
and an organisation trying to execute strategies under these circumstances 
assumes that they can either get by with managers or sub-optimal leadership or 
that achieving just a part of their initial objectives will create the momentum, 
which will then be able to drive through the rest of the strategy. In reality, 
this is not the case; one critical misstep can jeopardise an entire investment.<br>
<br>
In the longer term, a persistent leadership gap will be responsible for an 
inexorable decline in the ability of an organisation to sustain performance and 
rise to new challenges. Such organisations create a vicious cycle in which good 
leaders become overextended or are moved haphazardly, and so there is no time to 
develop new leaders, which adds to the pressure, and so it goes on and on. 
Finally, the time comes when they hand over reins to a less-experienced, 
ill-prepared successors and the organisation puts its core operations and 
strategic growth at risk. Given the severe consequences of a leadership gap, the 
best-planned strategy is no more than wishful thinking if it can’t be moved from 
concept to reality. Why do so many organisations discover their leadership 
shortfall only when executing their strategies? A clear picture of the 
leadership gap should help guide strategic thinking. Thinking about leadership 
upfront can affect the direction, the path, and the actual outcome of a 
strategy.<br>
<br>
Proper strategy will not succeed in a void and leadership often makes the 
difference between merely reaching for great opportunities and actually 
achieving their potential. Top managers must assess their organisation’s 
management and leadership needs, and find ways to close it over the short, 
medium and long term. Better still, they should integrate leadership development 
with strategic planning and thoughtfully match their need for leaders with what 
the organisation needs. </font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
