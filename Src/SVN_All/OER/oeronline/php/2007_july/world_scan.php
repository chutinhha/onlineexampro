<font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">Global energy consumption continues to grow<br>
</font></b><font face="Verdana" size="1">The year 2006 saw high and volatile energy prices. But despite high prices, 
world energy consumption growth remained above average in keeping with recent 
trends. Energy use is also increasingly shifting away from OECD countries and 
becoming more carbon-intensive. These are the main findings from the BP 
Statistical Review of World Energy 2007. It was a year when energy markets were 
once again the centre of attention, attracting the interest of politicians, 
consumers and policy-makers alike. “Last year showed markets at work. Primary 
energy consumption growth has decelerated – particularly for fuels which have 
seen the highest increase in price,” said BP’s chief economist-designate 
Christof Rühl. “However, global carbon intensity – the link between carbon 
emissions growth and energy growth – has increased.” For the second year in a 
row, world energy growth slowed, rising by 2.4 per cent, down from 3.2 per cent 
in 2005, but still just above the 10-year average. The pattern of recent years, 
which has seen robust demand in Asia Pacific and China in particular, was 
repeated with Chinese energy consumption rising more than 8 per cent. China’s 
usage of all forms of energy rose in the year, taking the country’s share of 
total global consumption to more than 15 per cent.<br>
