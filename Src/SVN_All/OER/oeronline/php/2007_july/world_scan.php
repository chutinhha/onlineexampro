<p align="justify" style="margin: 3px 10px">
<b><font face="Verdana" size="2" color="#CC3300">US economy growth slows down to 0.7 per cent in Q1 <br>
</font></b><font face="Verdana" size="1">The US economy grew at an annual rate of 0.7 per cent in the first quarter of 
2007, much slower than the 2.5 per cent pace in the final quarter of last year, 
marking the weakest showing in more than four years. The growth rate in real 
gross domestic product – the output of goods and services produced by labour and 
property located in the United States – was slightly higher than the 0.6 per 
cent pace previously estimated. The final reading disappointed economists. The 
deceleration in real GDP growth in the first quarter primarily reflected an 
upturn in imports, a deceleration in exports, a downturn in federal government 
spending, and a deceleration in personal consumption expenditures for non-durable goods.
