<img border="0" src="wc1.jpg" width="175" height="175" align="right" hspace="6" vspace="4">Airline industry profitability improves<br>
</font></b><font face="Verdana" size="1">The International Air Transport Association (IATA) has announced a US$5 billion 
projected profitability for airlines in 2007. IATA Director General and CEO 
Giovanni Bisignani said, “The industry has changed tremendously in five years. 
Labour productivity is up 56 per cent. Distribution costs are down 13 per cent. 
Non-fuel unit costs dropped 15 per cent and load factors are at record highs – 
76 per cent in 2006. Airlines needed an oil price of less than US$20 per barrel 
in 2002 to break even. Today we are profitable at nearly US$70 per barrel.” 
While the results are encouraging, Bisignani also sounded a word of caution. 
“Airlines are a US$470-billion industry. A profit of US$5 billion is peanuts. We 
need US$40 billion just to cover the cost of capital. The industry is moving in 
the right direction, but with US$200 billion of debt, the financial hole is 
deep. The challenge is to turn the peanuts into sustainable profits.”<br>
