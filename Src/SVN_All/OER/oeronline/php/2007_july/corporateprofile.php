<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="4">
<b>Leading the charge<br>
</b>
</font><font face="Verdana" size="1">
<b>Set to commence full operations in the third quarter of 2008, Sohar Aluminium is 
paving the way for an industrial future. A report by P Aneel Kumar<br>
</b>
<br>
<img border="0" src="cp.jpg" width="300" height="256" align="left" hspace="6" vspace="4">In line with the Sultanate’s vision to becoming less dependent on oil-based 
industry, a number of companies in varied industries are being floated. One such 
company that has come up with great promise in the manufacturing sector is Sohar 
Aluminium. Considered to be the first greenfield aluminium smelter project in 
the region for over 25 years, Sohar Aluminium was formed in September 2004. <br>
<br>
As aluminium smelting offers an opportunity to develop both gas-based and 
manufacturing industries, the project is in line with the Oman government’s 
strategy to support aluminium production as a sector for development.<br>
<br>
With an ambitious target of starting first hot metal production during Q2 of 
2008 and full production by Q3 of 2008, the project will have a 350,000-tonne 
smelter capacity. “We are in a strong position to bring the project on schedule 
and on budget. Thankfully, our sites were not affected by Cyclone Gonu. However, 
some suppliers and contractors have experienced small delays,” said Tony 
Kinsman, CEO, Sohar Aluminium. As the smelter plant requires large amount of 
electricity, the company is also building a 1,000-MW gas-fired power plant in 
Sohar Port. <br>
<br>
Apart from these two, the third project is on port side of the harbour area, 
where a huge storage facility for raw material is being built. “The construction 
contracts were awarded some 18 months ago. At the end of May, the construction 
on smelter and port projects was approximately 30-40 per cent completed. We have 
already largely finished engineering and procurement activity,” said Kinsman.<br>
<br>
<b>Latest technology<br>
<img border="0" src="cp1.jpg" width="212" height="239" align="right" hspace="6" vspace="4"></b>The project would be one potline initially. Once the availability of gas is 
eased out, the company plans to go for second potline with additional 
350,000-tonne capacity. “We will employ the AP 35 technology, which is a 
progression of the very successful AP30 technology and in fact we will be the 
first commercialisation of the AP35 technology in the world. We can proudly say 
that we have set global benchmark in this regard,” the CEO said. For the first 
potline, Oman Oil and ADWEA each own a 40 per cent share of the company and 
Alcan the remaining 20 per cent. The size of the project is US$2.4 billion.<br>
<br>
The company currently has 17 major contractors working on various parts of the 
project. For example, Carillion is carrying out all earthworks for the smelter 
and port sites. “We have awarded more than US$200 million in contracts to local 
companies and, in some cases, these contracts will be ongoing once we begin 
operations. A further 60 or more contracts are still to be awarded to local 
businesses, so I see Sohar Aluminium continuing to be a primary force in Oman’s 
new manufacturing industry,” Kinsman said. <br>
<br>
For the last 18 months, the company has been recruiting for its operations team 
and will continue to do so in the next 12 months. The company has currently 
about 450 employees, including 115 trainees, in the team. Another 600 would be 
recruited soon. The Omanisation level is at almost 50 per cent. On the 
construction front, about 6,000 workers are on the smelter plant job and another 
2,000 on the power plant. Here, the Omanisation level is at 34 per cent.<br>
<br>
One of the unique programmes introduced in the process is to “recruit 700 Omanis 
and offer them training in construction for six months followed by engagement in 
projects,” said Kinsman. “This programme, which is underway currently, has been 
designed in conjunction with the Ministry of Manpower. From these 700, some of 
them will be offered jobs in our operations team for the smelter and the rest 
will be able to use their new skills to get jobs in the booming construction 
sector in the local region or elsewhere in the Gulf,” he explained.<br>
<br>
For the community in the region, the company is in the process of establishing a 
new international school in the Batinah region, for English speaking students in 
the region. On the operations front, the company expects to sell 60 per cent of 
its production domestically and export the rest.<br>
<br>
<b>
<img border="0" src="cp2.jpg" width="300" height="225" align="left" hspace="6" vspace="4">Creating opportunities</b><br>
In May, Sohar Aluminium signed its first local liquid metal sales agreement. As 
part of the agreement, the company will supply SAGTAK, a joint venture between 
the soon-to-be-formed Omani company, Takamul Investments, and Austrian 
Salzburger Aluminium AG (SAG), with 20,000 tonnes of liquid aluminium metal per 
year. With SAGTAK, the company aims to develop a downstream ‘aluminium cluster’ 
within Oman. “The sale of this metal to a local company, rather than exporting, 
will result in additional employment opportunities and will generate new income 
streams for the companies involved as well as for the people in the Batinah 
regions,” Kinsman said.<br>
<br>
SAGTAK will be strategically situated in a specially designed downstream 
industrial park, adjacent to the Sohar Aluminium smelter. The metal can then be 
easily transported in liquid form from Sohar Aluminium to the SAGTAK casthouse, 
where a value-added product in the form of aluminium busbars will be produced. 
These busbars will then be sold to third parties. Both the parties will benefit 
from this as Sohar Aluminium will not have to solidify the metal prior to 
delivery to a customer and SAGTAK will not have to remelt the metal prior to 
producing the product. A number of other projects are also being investigated.<br>
<br>
To add value and create further business opportunities, Sohar Aluminium is 
seeking other companies to come and set up their processing facilities and use 
its aluminium. The company also has a sales and marketing agreement with its 
shareholder Alcan for exports.<br>
<br>
If all goes well, the company’s impact on Oman’s economy is going to be 
significant. “Not only is the company providing skilled employment for young 
Omanis but also introducing a new industry that will have far-reaching benefits 
for all sectors of the community in this country,” the chief
executive said.<br>
<br>
All done, the company is set to lead the charge of Oman’s new manufacturing 
industry. 
