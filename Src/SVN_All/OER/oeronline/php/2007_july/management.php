<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Coach K on Leadership<br>
</font></b><font face="Verdana" size="1"><b>Managing a basketball team has lessons for team managers 
in other spectrums. One of the most successful managers of our times tells us 
more<br>
</b>
<br>
<img border="0" src="manage2.jpg" width="150" height="192" align="left" hspace="6">Mike Krzyzewski (Coach K) is head coach of Duke University’s men’s basketball 
team and the executive-in-residence for the Center of Leadership and Ethics at 
the Fuqua School of Business. He is widely recognised for his leadership – 
leading Duke to three national championship victories and 10 Final Four 
appearances and being named National Coach of the Year 12 times in eight 
different seasons. Author of the New York Times bestseller “Leading With the 
Heart” and the forthcoming “Beyond Basketball – Coach K’s Keywords to Success” 
(Warner Books), he is a frequent motivational speaker for many Fortune
500 companies.<br>
<br>
<b>How do you define leadership?</b><br>
First of all, you cannot have one definition of a 
leader. People always want to put others in a box and say this is what a leader 
is or isn’t. To me, 
leadership is shown in so many different ways that you have to get to know your 
people and find out what type of leaders you have. A vocal leader can be 
somebody who has the courage to stand up in a meeting and say, “Coach, I don’t 
understand what you said” or “Hey, guys, what Coach means is this.” But 
leadership doesn’t have to be fiery. A simple statement by someone with courage 
in a moment when people are together is sufficient.<br>
<br>
<b>How much do you delegate, and how hands-on or hands-off do you need to be in 
order to cultivate other leaders? </b> <br>
First of all, you give them responsibilities that will put them in a position 
where they have to lead. You don’t micromanage. You empower your personnel to 
make decisions and then let them develop a course of action in how to get their 
people to that decision in a positive way. You can’t give them responsibilities 
without empowering them to use their capabilities, their leadership, and their 
techniques to accomplish a goal.<br>
<br>
<b>(Duke assistant coach) Steve Wojciechowski is teaching post defence. He’ll come 
up with his own drills and plans and we’ll discuss from different perspectives – 
we hadn’t thought of that, did we think of this, why don’t we
try that?</b><br>
When you empower you create ownership, and that is critical in the development 
of a total team. If my players are playing for me, we will never achieve the 
level that we can. If the players play because of us, we will have a greater 
chance of being who we can be.<br>
<br>
<b>
<img border="0" src="manage3.jpg" align="right" hspace="6" vspace="4">You have these players coming from high school, where they were the best of the 
best in their world, and now they’re here and there are others who are better 
than them. How do you deal with the human dynamics of that?</b><br>
I remind each player: “Each of you will run your own race. Don’t judge yourself 
by the race another one of your classmates is running.”<br>
<br>
A youngster can get fixated on his own race with someone else, and not the race 
that the team is on. So I reassure them that things will happen well. The best 
example of a great race that was run by two players would be Elton Brand and 
Shane Battier – who came in at the same time. Elton’s physical capabilities were 
more advanced, and by the end of the sophomore year, he went pro and was the No. 
1 pick overall and the college player of the year. Shane was a starter, but not 
an all-American. In his next two years, he became an all-American and the 
college player of the year. His race took four years. Elton’s took two.<br>
<br>
Sometimes it doesn’t turn out that well. A player is constantly looking at 
another player as the barometer of how successful he is, and he might transfer 
or never achieve the level that he could have achieved because he never felt 
good about himself, no matter what you said to him. Those are the dynamics that 
go on in the workplace and on teams all the time, and sometimes you can control 
them and sometimes you can’t.<br>
<br>
So you have to be very careful about how you bring people along and the 
competition that they see within their own class or within the team or within 
the business.<br>
<br>
<b>Every talk we’ve heard you give, you’ve always mentioned your family. How do you 
use your family as a model for providing context for your team, in terms of 
modelling leadership?<br>
</b>Winning basketball games is important, but the most important thing is how you 
take care of your family. I learned that from my mom, who was always there. 
That’s the kind of safety net I want to give my family and it’s what they give 
back to me.<br>
<br>
There’s no way that I can fail in the eyes of my family as long as I’m doing 
everything in an ethical manner. I can lose games and still be dad, granddad, 
husband – that’s not going to shake the foundation. The result of my actions in 
my business life will never weaken my family strength.<br>
