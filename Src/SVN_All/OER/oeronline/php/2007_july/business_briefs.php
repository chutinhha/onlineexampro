<img border="0" src="bus1.jpg" width="175" height="177" align="left" hspace="6" vspace="4">Oman Air in strategic tie-up with India’s SpiceJet</font></b><font face="Verdana" size="1"><br>
Since Oman’s pullout from Gulf Air over a month ago and as part of its expansion 
strategies, Oman Air has recently added four new destinations including Abu 
Dhabi in UAE, Chittagong in Bangladesh and Lucknow and Jaipur in India. The 
airline has added two more new aircrafts to its fleet. Oman Air has also 
designated a group of young school leavers to undergo full Joint Aviation 
