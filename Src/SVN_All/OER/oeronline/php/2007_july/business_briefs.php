UK. Captain Hamed Al Jabry, Senior Manager-Line Operations, Oman Air said: “As 
the Omani cadet pilots are getting ready to take the skies, Oman Air looked for 
a partner who can provide us with quality and well trained Boeing 737 pilots. 
Spicejet, a leading airline in India, has been finalised by Oman Air for 
partnership. We entered into a strategic tie-up with Spicejet after an extensive 
and careful inspection. Their pilots will fly with Oman Air on a contract basis 
for a period of one year.”<br>
