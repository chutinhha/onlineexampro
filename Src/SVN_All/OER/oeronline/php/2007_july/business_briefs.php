<b><font face="Verdana" size="2" color="#CC3300">BankMuscat launches interest-free Al A’ Awn loan<br>
</font></b><font face="Verdana" size="1">With the aim of supporting its customers towards rehabilitation process after 
cyclone Gonu hit the Sultanate, BankMuscat has launched ‘zero per cent’ interest 
‘Al A’Awn’ (support) loans. As the name suggests, the intention of ‘Al A’Awn’ is 
to provide support to cyclone victims and will be available to all existing 
BankMuscat customers in the Muscat Governorate and Sharqiyah who have their 
salaries assigned to the bank with a monthly salary of RO500 or lower and will 
be available till August 15, 2007, said AbdulRazak Ali Issa, Chief Executive of 
the bank.<br>
<br>
Loans of up to three times one’s salary will be credited directly to the loan 
account of the person in question within 24 hours of receipt of the completed 
loan application form. No bank charges to be levied and loans are repayable over 
one year or then combined with existing loans. AbdulRazak said: “The recent 
cyclone has wreaked havoc across the nation. There are thousands of people in 
dire need of support and assistance. The zero per cent Al A’Awn loans, we 
believe, will help over 20,000 of our customers nationwide for rebuilding their 
lives.” <br>
