<b><font face="Verdana" size="2" color="#CC3300">PDO announces Mabrouk full field development<br>
</font></b><font face="Verdana" size="1">Petroleum Development Oman (PDO) has announced that it received shareholder 
approval to go ahead with the full development of the Mabrouk field. The new 
field development plan, which was discovered in 1979 and is currently producing 
around 8,000 barrels a day, calls for the drilling of up to 80 new wells and the 
construction of a new production station and gathering system. The project is 
being fast-tracked so that the additional incremental production is expected on 
stream in late 2009. When all the new hardware has been fully commissioned, the 
field is expected to produce around 15,000-20,000 barrels a day of oil and 1-2 
million cubic metres a day of gas. The gas will be delivered to the Saih Rawl 
gas processing plant where it will enter the Government Gas System. According to 
PDO, just 1 per cent of the oil-in-place at Mabrouk has been produced so far and 
the full-field development plan will increase the recovery factor to
around 10-12 per cent.<br>
