<img border="0" src="bus2.jpg" width="300" height="144" align="right" hspace="6" vspace="4">Al Omaniya launches RO8-mn bond issue</font></b><font face="Verdana" size="1"><br>
Al Omaniya Financial Services SAOG has launched its RO8 million issue of secured 
compulsorily convertible bonds on rights basis. Aftab Patel, Chief Executive 
Officer, Al Omaniya Financial Services, said the issue will close on July 2. 
Oman Arab Bank is the collecting banker. The face value of the bond is RO1 plus 
an issue expense of 20 baisas per bond. The tenor of the bond is two years at a 
coupon rate of 7.5 per cent per annum. Interest will be paid to the bondholders 
every six months. These bonds will be converted into shares at 82.5 per cent of 
average stock price for the three-month period just before conversion.<br>
<br>
The company has achieved a pre-tax net profit of RO1,049,000 for the five months 
ending May 2007, and an NPA coverage of 148 per cent. The company is expected to 
achieve a net profit growth of about 15 per cent, giving an EPS of 317 baisas 
for year 2007 over 2006.<br>
