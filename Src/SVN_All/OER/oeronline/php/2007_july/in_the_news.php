<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The storm and after<br>
</font></b><font face="Verdana" size="1"> <b>Sunil Kumar Singh takes an overview of the losses suffered by Oman Inc and the 
resilience it showed in the aftermath of the 6/6 deluge</b></font><p align="center" style="margin: 3px 10px">
<br>
<font face="Verdana" size="1"> 
<img border="0" src="news5.jpg" width="450" height="300"></font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"> <br>
<br>
The June 6 disaster, when Cyclone Gonu struck Muscat, Sharqiya and coastal 
regions, was one that had not been factored in by most business establishments 
in Oman. In fact, while some businesses heaved a sigh of relief as the losses 
they suffered had insurance backup, others incurred heavy losses, while still 
others cushioned the impact through timely pre-emptive steps.<br>
<br>
But while the exact figure of losses incurred by Oman’s corporate sector is yet 
to be ascertained, preliminary findings, based on company’s disclosures to the 
Capital Market Authority, indicate that in most of the cases the damages are not 
major; most companies had suitable insurance cover, and hence do not foresee any 
significant financial impact. The disclosure to the CMA (see table in the next 
page) followed a directive issued by the latter to all companies listed on the 
Muscat Securities Market.<br>
<br>
Companies that have denied suffering any major damage or reporting adequate 
insurance cover include Oman Hotels &amp; Tourism Co, Gulf International Chemicals, 
National Gas Co, AES Barka, Al Ahlia Converting Ind, Port Services Corporation 
and Dhofar Insurance.<br>
<br>
<img border="0" src="news4.jpg" width="300" height="202" align="right">Insurance Companies like Al Madina Gulf Insurance Co, Oman, The New India 
Assurance Co Ltd, AXA Insurance, Al Ahlia Insurance Co, Royal &amp; Sun Alliance 
Insurance Oman and Muscat Insurance Co contacted by the publication, too have 
acknowledged that since they are protected against reinsurance, the flood claims 
by their customers would not financially affect them considerably. As Andrew 
Woodward, GM, Muscat Insurance Co comments, “Our company has in place a 
comprehensive programme of reinsurance, by means of which we will be able to 
control our ultimate net losses, and ensure financial security for us and for 
our customers.” Rejoins Ravi Shankar, GM, Al Madina Gulf Insurance Co, “Most of 
the impact on these claims will be taken care by our Treaty arrangements. As a 
result financially our operation will not suffer on account of these losses.”<br>
<br>
And proving the adage ‘prevention is better than cure’ were companies like the 
retail major Muscat City Centre (MCC), which averted major losses by 
implementing a well-planned crisis management programme. “With this effective 
emergency programme, we were able to tackle the consequences of the cyclone and 
recoup normalcy in the mall almost immediately. In fact, just 48 hours after the 
cyclone, on Friday, MCC was the only mall that was fully operational. This 
enabled Carrefour to immediately open their store to provide people with 
necessities and additional supplies of food stuff and groceries,” said Ibrahim 
Al Qasmi, General Manager, MCC.<br>
<br>
<img border="0" src="news3.jpg" width="300" height="221" align="left" hspace="6" vspace="4">Similarly, Grand Hyatt, Muscat, remained open all through the cyclone. According 
to Patrick Iserlohe, General Manager: “Despite some minimal damage to the hotel 
property and some cancellations and lost business during the cyclone period and 
immediately after, the hotel was able to restore regular operations of all its 
facilities from the week immediately after the cyclone.”<br>
<br>
Like the Grand Hyatt, Shangri-La’s Barr Al Jissah too escaped the wrath of the 
cyclone and did not suffer any structural damage to any of its three hotels, 
with only the beach areas needing to be cleaned up. Said its General Manager, 
Desmond Hatton, “Electricity supply was disrupted, but essential services were 
made available in all our three hotels. The external power supply has since been 
reconnected. The water supply was also not affected as we have our own 
desalination plant. All facilities and services are now fully restored.”<br>
<br>
Even beachfront properties like ‘The Wave’ in Muscat remained unscathed from the 
lashes of the cyclone. As Amer Mustafa Al Fadhil, Vice President (External 
Affairs), put it: “We’ve made provision of artificial lakes in our project. 
Whilst these provide for beautiful lakefront properties, they also double up as 
a flood-absorption system. These lakes have been designed to absorb the 
floodwater and then discharge it into the sea through pre-designated paths. In 
the recent cyclone, these lakes worked tremendously and our construction site 
remained untouched.” However, some hotels like The Chedi and Inter Continental 
Muscat had to suffer some losses, and The Chedi is expected to resume its 
operation within a short span of time.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px"><font face="Verdana" size="1"> 
<img border="0" src="news1.jpg" width="496" height="599"></font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"> 
<br>
<b>Faced the brunt</b><br>
While not significantly affecting the country’s core oil and gas sector, Cyclone 
Gonu brought all oil exports to a halt for two days. According to the Oxford 
Business Group, “It was not until June 9 that tankers began loading at the Mina 
al Fahal oil export terminal or production resumed at the Qalhat facility, 
operated by Oman LNG and Qalhat LNG, with other refineries returning to near 
normal operations
soon after.” <br>
<br>
On its part, Oman LNG said it shut down its Sur-based production facilities on 
June 5, a day before the cyclone struck, as a precautionary measure thereby 
ensuring minimal damage. In fact, the company has reported that the cyclone 
passed with no injury to employees either at the plant or in the adjoining 
residential areas. Says Dr. Brian Buckley, GM and Chief Executive, Oman LNG, 
“Our preparations to secure the plant prior to the cyclone paid off, with no 
serious damage to our facilities in Sur and no serious interruption in our LNG 
supply. No damage was reported in our critical liquefaction or utilities’ 
equipment.”<br>
<br>
<img border="0" src="news2.jpg" width="300" height="268" align="left" hspace="6" vspace="4">Another sector that took prompt precautionary measures, following up on the 
warnings issued by the ROP/authorities, was the ports and shipping sector. The 
timely action taken by the Port Services Corporation in reducing the stack 
height of containers stored at the Port Sultan Qaboos and ordering the closure 
of the port for three days, supported by proper updates to the shipping 
community, helped avert any major crisis to the shipping industry in Muscat, 
which is the gateway to the country. Again while Oman International Container 
Terminal (OICT), Sohar, was shut down, the Salalah port functioned normally. 
According to James Frater, CEO, OICT, since the cyclone changed direction and 
headed north towards Iran, Sohar did not experience the strong winds or the 
heavy rain that hit Muscat. Nevertheless, OICT had taken precautionary measures, 
such as placing storm anchor pins and tying downs in its Quay Cranes, among 
others to avoid
cargo damage.<br>
Shipping activities, though, were partly affected. Says Sunil Joseph, Country 
Manager, Maersk Shipping Services &amp; Co LLC, “During the week following the 
cyclone, we missed vessel calls at the Muscat and Sohar ports, which affected 
export as well as import traffic. Following the resumption of operations on June 
9, we found that the ROP/ customs systems had been affected as a result of the 
cyclone and this impacted operations. Nevertheless, manual operations by 
ROP/customs officials helped restore normalcy.”<br>
<br>
For banking major, BankMuscat, its business continuity planning (BCP) initiative 
proved to be its saviour. Explaining the rationale behind this initiative and 
the level of preparations, Said Al Badai, chairman of the bank’s BCP committee, 
informed, “We were prepared for the worst as specialised teams had rehearsed 
various scenarios. Though we were impacted by power and data connectivity 
failure, we managed to reach out and serve our customers across the network.” 
The bank’s data recovery centre in MBD South was pressed into service after the 
office in the Qurum Commercial Complex was severely damaged. <br>
<br>
But there were companies that suffered enormous damage – that is still being 
evaluated – due to logistical failure arising from the cyclone or due to 
flooding. Pradeep Koppikar, GM-Corporate, Bahwan Engineering Company LLC (BEC), 
said, “the BEC headquarters at Ghubra suffered extensive damage due to flooding 
of our workshops, completely submerging our server, computers and all 
communication network. The company’s operations directly affected were finance, 
commercial, administration, HR, IT, elevators, and electrical and mechanical 
products. It took over 200 persons, working 18 hours a day. to enable us to 
enter our premises on June 18.” <br>
<br>
<img border="0" src="news6.jpg" width="300" height="256" align="right">Other sectors that suffered losses were aviation and logistics. As Peter 
Schilter, Manager - Representation Oman, Swiss International Airlines Ltd, 
commented, “The cyclone affected us commercially. Swiss International Airlines 
had to cancel two flights on June 6 and 7, and we had to re-book approximately 
150 passengers. While we found space on other flights for about 100 passengers, 
we had to rebook 50 to other airlines. We estimate the loss in revenue at RO 
18,000. The sales office, though, was open for passenger service on all days 
during the storm.” <br>
<br>
Echoes Salim Khoury, country manager, Aramex - Muscat, a major logistics 
company, “With regards to our business, Aramex was affected as office telephone 
lines and internet connections were disconnected for almost two weeks, adversely 
affecting our collections and deliveries to our clients. Moreover, some of our 
clients had to relocate to new safer areas, without any proper set up, creating 
a communication gap. As a result, shipments kept piling up in our office.”<br>
<br>
Agility, another logistics major also faced crises such as stoppage of water 
supply and non-functional ACs and IT systems due to power outage for five days. 
Says Chris Clark, country manager, “The biggest single issue we had in our 
facility at Ghala was delays in making deliveries. To get to the Qurum 
roundabout from Ghala took up to three hours and this meant we had to try and 
hire additional trucks to tackle the backlog of orders.”<br>
<br>
<b>Impact on key sectors</b><br>
The BankMuscat Research Team* has come up with its analysis on the directional 
impact of the cyclone on various sectors. According to the analysis, “it is 
difficult to quantify the amount of loss at this juncture. Nevertheless, 
companies like Al Anwar Ceramics, National Aluminium, Oman Cables are expected 
to benefit as reconstruction picks up but given the export led growth models of 
these companies, the overall impact on them could be limited. Similarly, the 
cement sector offtake is expected to increase in north Oman as reconstruction 
begins. The telecom sector is to have limited financial impact in terms of asset 
damage. Oil marketing companies will be positively impacted due to higher fuel 
offtake as a result of congestion on damaged roads and negatively impacted due 
to lesser number of vehicles (a number of them have been damaged by the 
cyclone).”<br>
&nbsp;<br>
As regards the impact on the leasing sector, the report stated, ”since a major 
business in leasing is auto financing, we expect the industry to be severely 
impacted, as a number of automobiles have been damaged in the floods around the 
country. As per media reports, 10,000 cars have been damaged in Muscat city 
alone. Since a majority of auto insurance policies do not cover damage due to 
flooding, we expect the leasing companies to take a hit in the event of total 
damage to their collaterals, especially when customers fail to pay their dues 
and collaterals have no residual value. However, in the long term, we believe 
that reconstruction activity will stimulate demand for construction equipment 
and replacement demand for automobiles and consumer durables will drive 
increased volumes, going forward.”<br>
<br>
Regarding the impact on the banking sector, the report adds, “the immediate 
impact on banks would be loss in value of the collaterals, thereby increasing 
risk of existing loan facilities. Furthermore, we do not expect affected people 
to pay their regular EMIs for the next 2-3 months, which will put even personal 
loans under risk. However, over the long-term, reconstruction and replacement 
demand could drive increased credit offtake, which is positive for the banking 
sector in general.” <br>
<br>
As regards the insurance sector, the report stated, ”The widespread damage to 
property will impact the insurance companies the most due to large-scale claims 
arising from property and automobile policies. However, the industry believes 
that since most of the automobile policies in Oman do not have the Storm, 
Tempest, Flood (STF) coverage, the insurance companies are not liable to pay 
damages on account of this. Against this, almost all property insurances have a 
STF coverage, which makes insurance companies liable. Since 90 per cent of all 
property risks in Oman are reinsured, the impact of these claims might not be 
substantial for local insurance companies. Despite this, we believe that the 
short-term impact depends on the nature of reinsurance contract of each 
insurance company and the quality and adequacy of reinsurance it has procured. 
In the long run, we expect reinsurance rates to go up, which will negatively 
impact the profitability of local insurers, as we do not expect them to be able 
to pass on the entire cost to the consumers, due to competition.”<br>
<br>
Cyclone Gonu, a meteorological rarity, was not only about damages and losses but 
also about business interruptions. Nevertheless, with each passing day, Oman’s 
corporate sector’s faith in success is getting far greater than its fear of 
failure following the cyclone. The business mood may have stumbled on account of 
6/6, but it is getting up, dusting itself and will soon be ready to canter 
again. <br>
<br>
(*The views in this report are the views of the BankMuscat Research team and do 
not necessarily reflect the views of the Bank as a whole.) </font>
