<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
		</font><b><font face="Verdana" size="4">Fuzzy to focus<br>
		</font></b><font face="Verdana" size="1"><b>More opportunities are leading to more competition, leaving marketers grappling 
with the question of how to sustain, improvise and prolong the life of their 
brand(s). Akshay Bhatnagar profiles Bahrain-headquartered Vahid Associates Brand 
Futurists, which promises to address this challenge with
its unique methodologies<br>
</b>
<br>
<img border="0" src="marketing7.jpg" width="149" height="142" align="left" hspace="6" vspace="4">Many brand managers whine that there is not much to choose when it comes to 
appointing an advertising agency or a brand consultancy, as their deliveries are 
not really distinct. So, when Indranil Gupta, group consultant of Vahid 
Associates Brand Futurists (and CEO of group company, Contexture Associates) 
says that the firm focuses on identifying the prevailing brand values of an 
organisation and its components, and ensuring optimal delivery of brand 
recognition for its clients, I thought here goes another clichéd statement. But 
when I asked him how differently it did it, his reply was an eye-opener. <br>
<br>
“Vahid Associates’ uniqueness comes from its specialisation in being able to 
deal with issues of the future in business. While most consultants in branding 
and marketing use the rear-view mirror to create strategies and plans, we, as 
brand futurists, use a ‘fuzzy-to-focus’ concept to assimilate and project future 
scenarios. We paint the picture of future possibilities for our clients and once 
we have their participation, input and agreement on that vision, we construct an 
effective bridge that shows the way through accurate communication (see case 
studies in the next page). This is highly interactive planning methodology that 
works like a compass to allow our clients to determine – with our experience, 
guidance and advice – where and how they should plan and conquer tomorrow’s 
competitive world. We go beyond practicing benchmarking to advocating 
bench-trending,” Gupta says. <br>
<br>
“Our kind of work not only addresses the predictable issues of communication 
deliverables that any ad agency handles, but goes beyond into aspects of 
business and management consulting, HR and motivational training and strategic 
thinking… the more integrated, holistic and in-context communications 
solutions.” The brand master and strategic marketing communications’ consultancy 
firm is based in Bahrain, with a branch office in Dubai and network affiliates 
in Qatar, Saudi Arabia, Iran and India. Its client portfolio includes Ahli 
United Bank, Bahrain Post, Batelco, Bateel International, Maya Le Chocolaterie, 
National Bank of Bahrain, Qatar Steel, Rajhi Holdings (KSA), Saudi Aramco and 
Schlumberger, among others.<br>
<br>
The driving force behind the firm is its executive principal and chief brand 
futurist Vahid Mehrinfar. In a career spanning over 26 years, he has earned 
international recognition through his work in a variety of projects, ranging 
from art direction for film production, corporate promotional illustrations, 
rendering visualisations of palace interiors and private jets, architectural 
renditions of mega-development projects to creation of brands (see interview 
with Vahid). On the growth front, Gupta says, “In an ad market that is supposed 
to have flattened out in the last year, we grew by 25 per cent. Our reputation 
is galloping ahead thanks to the promotional aura around the brand Vahid that is 
spreading like wildfire, and our ‘growth through referrals’ strategy has spurred 
our business to higher level. A large proportion of indigenous organisations are 
going into re-branding in some way or another. It helps to be positioned at the 
helm of the “doctors of branding” ladder! Branding projects put us in close 
contact with the top echelons in various client organisations. And the trust 
that emanates from such a position drives a more holistic relationship of 
longevity. <br>
<br>
“We, therefore, are fortunate to have a set of businesses whose organic growth 
has fuelled our revenues, particularly in the banking sector as well as the real 
estate, large private corporate houses and government institutional arenas. We 
have a strong regional clientele outside the shores of Bahrain and our focus is 
today, again through referrals, on leveraging our network even in the far-off 
US, Europe and Middle East to bag prestigious projects that make us increasingly 
more global in profile.”<br>
<br>
Some of the prominent brand identity and communication turnarounds engineered by 
Vahid Associates includes: <br>
<br>
<b>
<img border="0" src="haya.jpg" width="200" height="247" align="left" hspace="6" vspace="4">Haya Rashed Al-Khalifa Law Firm<br>
</b>When she was contesting the election to become the president of the 61st United 
Nations General Assembly, Sheikha Haya Rashed Al Khalifa approached Vahid 
Associates (VA) to brand her law firm, which currently enjoys a leading position 
in the Kingdom of Bahrain. Being the visionary she is, and her understanding of 
the adage ‘pay now or pay later’, her brief propagated the need for a formidable 
and contemporary brand image in a faceless and conservative industry from a 
branding context. The firm, which offered a complete range of legal services 
along with a distinguished reputation for the quality of its legal and 
professional advice, needed a fresh, positive and innovative look.<br>
<br>
The challenge was to elicit an image that was international in its outlook but 
with homegrown expertise. A series of brand workshops were conducted and VA took 
the opportunity to unleash its unique methodology through its interactive 
process (whereby the client themselves were facilitated in reaching their 
desired brand position). From the current stance of a ‘master’ which they are, 
the desired essence was to be the ‘absolute’. This was manifested in the 
communication vehicles including identity, stationery, website, brochures, etc.<br>
<br>
The corporate identity symbolised the firm’s legal framework characteristics, 
i.e., defence, guardianship, honesty, professionalism, etc. A high profile event 
was launched with a special film on the organisation and its future endeavours. 
The brand today continues to be the icon of law firms in Bahrain and in the 
region, handling clients locally, regionally and internationally.<br>
<br>
<b>Qatar Steel</b><br>
<img border="0" src="marketing.jpg" align="right">VA helped launch the strategy for the future of Qatar Steel, transformed from 
the QASCO of yesterday–the new identity was revealed at a glittering event at 
the Marriott, Doha, on April 10 this year. Qatar Steel, the first integrated 
steel plant in the region, brought to the table three decades of outstanding 
performance and an unwavering reputation for quality and reliability. Yet, 
market environment had evolved in terms of customer expectations and active 
competition.   
<img border="0" src="marketing1.jpg" align="left" vspace="5">A need was identified, one of staying ahead of the market. The logo 
transformation was beyond a facelift – it portrayed the new culture of 
competitiveness and compatibility with a dynamic economic environment. The 
rationale was simple: if the company’s mission was to be at the forefront of 
Qatar’s development, the brand name must proudly carry the name Qatar. The logo 
conveys global, contemporary, yet enduring values, through pleasing form and 
bold colours. ‘We make steel matter’ – the expression underlines the pride 
behind the product, and reinforces a commitment to make the importance of steel 
resonate in the hearts and minds of people, and not fade into the background as 
a silent infrastructural component. This is reinforced by the internal slogan of 
‘Our Steel Shapes the Future’ – a belief that steel shapes the future of the 
company, its customers, the community, and the employees. Fresh promotional 
material was produced to complete the message of the fresh identity – an 
impressive array of graphic images were captured for the new corporate brochure; 
a corporate film wove together some of the best steel production footage to tell 
a stirring, heart-warming story of a traditionally cold metal; Syd Mead, the 
famous Hollywood futurist-designer, rendered a visualisation of Doha in 2050, 
from the eyes of Qatar Steel. This fresh, new perspective was extensively 
covered by the local press, general and trade publications, websites and 
television.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
