		<img border="0" src="marketing5.jpg" width="150" height="183" align="right" hspace="6" vspace="4">‘To become…not be’<br>
      </b>An alumni of Art Center of College of Design, Pasadena (US), and trained as an 
industrial designer, specialising in automotive and transportation design, Vahid 
Mehrinfar’s diverse experience spans many disciplines, with experience gained in 
North American, European and Middle Eastern markets. As executive principal and 
chief brand futurist of Vahid Associates Brand Futurists, he is empowering his 
clients in creating successful indigenous brands and excel in their respective 
domains. Akshay Bhatnagar catches up with him to find out more about his 
methodologies. <br>
      <br>
      <b>How will you define Vahid Mehrinfar as a brand?<br>
      </b>A pro-performance pragmatic purist where the fascination lies in the magic of 
the extraordinary… the fountainhead.<br>
      <br>
      <b>You wear many hats. What gives you the maximum satisfaction?<br>
      </b>The presentations, for whatever subject of design it may be, and then the 
result! Seeing it in person, as visualised, as a story to be told in vivid 
details of rendition, the actual merging of the dream into reality where time 
actually stops for a while, for it becomes obsolete!<br>
      <br>
      <b>You say that you help business leaders in painting their future. How do you do 
that?</b><br>
Not only they can’t generally see the future, they cannot even contemplate on it 
beyond business objectives. I help them to make their stories and then I empower 
them to sketch the scenes in reverse so that they can live with it.<br>
      <br>
      <b>Do you think your methodology is applicable to different brand in different 
product categories in different markets uniformly or you have to treat every 
problems/opportunity in a unique manner?<br>
      </b>The knowledge behind the methodologies I have developed remains consistent and 
is always subject to improvement, but they will always be applied to a spectrum 
of problems with diverse backgrounds and complex differences in their reasons 
for being a problem in the first place. Basically, I also design the problem!<br>
      <br>
      <b>What are your impressions about the brand management and marketing 
communications taking place in Oman? How Vahid the Brand Guru and your company 
can make a difference to the Omani market?</b><br>
Like most of the Middle East–near-East region, design is at its initial 
development stage in Oman as well. With the passage of time and the emergence of 
competition as a tool of advancement for the business community, the role of 
design in business is becoming more of a necessity rather than just being a good 
idea. In this equation, knowledge meeting purity of intent can prove to be a 
tremendous determining factor in business success. Vahid brings that to all his 
clients wherever they are and he will be all too honoured to do that for Oman 
also, for Omanis as a community represent a genuine symbol of purity and modesty 
in the region. Vahid hopes to be granted the permission to land his humble 
talent and design acumen in Oman.
