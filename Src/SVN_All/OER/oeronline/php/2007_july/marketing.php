<font face="Verdana" size="1"><br>
<br>
<img border="0" src="marketing4.jpg" width="205" height="311" align="left" hspace="6" vspace="4"><b>Bahrain Post</b><br>
<img border="0" src="marketing3.jpg" width="115" height="76" align="right" hspace="6" vspace="4">Historically, Bahrain Postal Directorate has enjoyed a long and rich heritage of 
service to the community and is intrinsically and inextricably linked to life in 
Bahrain. VA was approached to inject a new lease of life that would position it 
on par with high-end international postal services without losing its ethnicity. 
In March 2005, Bahrain Postal Directorate and Bahrain’s local post offices 
unveiled their new brand identity – ‘Bahrain Post’. The identity was a 
reflection of going beyond the mailing business to view itself in the wider 
scope of being in the business of deliveries and reaching out to people and 
connecting them with their needs. The new look clearly aimed to pay tribute to 
the past whilst becoming a symbol for Bahrain’s progressive future. The identity 
was launched in conjunction with the development and adoption of a new business 
model, and was the culmination of months of active participation by Bahrain 
Post’s employees and senior management, who came together at numerous workshops 
and seminars to align the various components for organisational strategy 
development. The new identity was seen as a viable and inspirational approach in 
meeting and exceeding its business goals – its renewed vision of greater 
service, greater delivery and greater care to its community. The launch of its 
identity was also in accordance with the new range of additional services, using 
the latest in technology and systems in line with international standards. </font>
