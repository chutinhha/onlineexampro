</font><b><font face="Verdana" size="2" color="#CC3300">All-new Captiva</font></b><font face="Verdana" size="1"><br>
        </font><b><font face="Verdana" size="2" color="#CC3300">
<img border="0" src="autonews1.jpg" align="right" hspace="6" vspace="4"></font></b><font face="Verdana" size="1">The all-new Chevrolet Captiva seven-seater is being rolled out in Oman. 
        According to a release, there are a number of derivatives in the range, 
        including the LS, LT and LTZ versions. The Captiva has a choice of two 
        engines – 2.4 L and 3.2 L – delivering 136hp and 230hp, respectively. 
        Customers can choose between front-wheel and all-wheel drive. The 
        vehicle is the first to offer ESP technology, which compares the 
        driver’s intended direction in steering and braking inputs to the 
        vehicle’s response and also integrates all-speed traction control. This 
        process senses drive-wheel slip under acceleration and individually 
        brakes the slipping wheels until control is regained. When on a steep 
        incline, Captiva’s Descent Control System (DCS) can be activated at the 
        press of a button to automatically slow down the vehicle without the 
        driver needing to press the brake pedal. This allows the driver to 
        concentrate entirely on steering the vehicle. Its exterior frame is over 
        4,600 mm long, 1,850 mm wide and 1,720 mm high and it sits on a 
        wheelbase of 2,705 mm.<br>
