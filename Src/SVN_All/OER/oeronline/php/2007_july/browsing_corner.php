        <p align="justify"><b><font face="Verdana" size="4"><br>
        Good is the enemy 
        of great</font><font face="Verdana" size="1"><i><br>
        </i>Why do some companies make the leap and others don’t? In their book 
        ‘Good to Great’, Jim Collins and his research team highlight not only 
        what makes a company great but also what sustains 
        that ‘greatness’ <i> <br>
        <br>
        </i>
        </font></b><font face="Verdana" size="1">
        <img border="0" src="book.jpg" width="175" height="247" align="left" hspace="6" vspace="4">Good to Great aims to clear some of the misconceptions we have with 
        regard to business. What is interesting about the book is that it not 
        only states the timeless principles of good to great in an easy manner 
        but in its tips on what works best for a successful business or company, 
        it clearly affirms what should not be done to avoid failure or only 
        one-time success. Therefore, along with your to-do list you are advised 
        to make a not-to-do list as well. <br>
        <br>
        Jim Collins is also the author of Built to Last and though it was 
        written first, he feels that Built to Last should be the sequel to Good 
        to Great. Jim and his research team identified companies that made the 
        leap from good results to great results, and managed to sustain such 
        performance for at least 15 years. In all, total study sets of 28 
        companies were studied to write Good to Great.<br>
        <br>
        The concepts have been developed by making empirical deductions directly 
        from the data collected, with the content emphasising on the building of 
        a theory from evidence rather than testing or proving an already 
        prevalent theory. Consider this: “We expected that good-to-great leaders 
        would begin by setting a new vision and strategy. We found, instead, 
        that they first got the right people on the bus, the wrong people off 
        the bus, and the right people in the right seats – and then they figured 
        out where to drive it. The old adage ‘people are your most important 
        assets’ turns out to be wrong. People are not your most important 
        assets. The right people are.”<br>
        <br>
        Collins breaks more myths. Every good-to-great company had “Level 5” 
        leadership during its pivotal transition years, where Level 1 is a 
        Highly Capable Individual, Level 2 is a Contributing Team Member, Level 
        3 is the Competent Manager, Level 4 is an Effective Leader, and Level 5 
        is the Executive, who builds enduring greatness through a paradoxical 
        blend of personal humility and professional will. One of the most 
        damaging trends in recent history is the tendency (specially of boards 
        of directors) to select dazzling, celebrity leaders and to de-select 
        potential Level 5 leaders.<br>
        <br>
        As you read along, one finds that the authors try to make people realise 
        that just being good is not good enough&#8722;you have to want to achieve 
        greatness. And, if this is not how you feel, probably you are engaged in 
        the wrong line of work. You have got to get yourself involved in 
        something you care so much about that you want to make it the greatest, 
        not because of what you will get but because it can be done. <br>
        <br>
        Each chapter starts with relevant quotes ending with a summary, complete 
        with all-important points in case you are in a hurry. One can always say 
        that what Collins says is nothing but common sense but still for a book 
        in the category of management, Good to Great makes good reading. 
        </font></p>
