<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">For Generations<br>
</font></b><font face="Verdana" size="1"><b>The flagship of Mecedes, new S-Class, continues to be a benchmark for other 
luxury sedans. Here is how</b><br>
<br>
<img border="0" src="auto.jpg" align="left" hspace="6" vspace="4">The name ’S-Class’ has been taken from the German word Sonderklasse. 
Sonderklasse means special class, and in automotive terms it refers to a 
specially outfitted car. Although used colloquially for decades, the ’S-Class’ 
was first officially applied in 1973 with the debut of the W116 model line.<br>
<br>
Ever since its introduction in the mid 1950s, it has remained as a benchmark for 
other luxury sedans anywhere in the world. In all honesty, the new S-Class is 
one of the most technologically advanced car in the market today. Being the 
flagship of the elegant and refined Mercedes armada gives us an idea as to how 
this car would be. With all its gadgets and gizmos, the S-class is something 
that comes across as mature as well-built.<br>
<br>
In dimensions, the new S-Class (W221) is 1.7 inches longer, almost an inch 
wider, and the wheelbase more than three-inches longer than the previous model. 
From the front it has same mighty grill with the three-pointed star, but from 
the side it does seem to look very unconventional from the traditional Mercedes 
trend and has incorporated some very significant curves. The priceless feeling 
of driving a Mercedes is still carried on and makes this a fine descendent.<br>
<br>
The rear end has been one of the most controversial aspects of the W221 with 
some claiming it is a copy of the E65 BMW 7-Series and is unattractive, while 
others saying it is bold, aggressive and, in fact, derived almost directly from 
the Mercedes Maybach. Important to note though that the Maybach was released 
about the same time as the 7 series (E65).<br>
<br>
We were given the S350L, the 3.7-Litre V6. Though we did have some initial 
reservations about it being a V6, these notions were soon removed as we pressed 
the accelerator pedal. The acceleration seemed smooth and constant throughout 
the powerband. You go further, you find that this was partially due to the new 
gearbox that is incorporated in the S-Class. The 7G-tronic seemed a bit overkill 
when it first appeared, but its execution can only be felt when depressing the 
pedal. The seven speeds with their gradual and smaller change in gear ratios – 
as compared to regular five or six speeds – give this car its overall smoothness 
in slow-driving or quick acceleration.<br>
<br>
The safety features on the S-Class have been more of an improvement on the 
current technologies. This should not be considered as a downside as till date 
Mercedes has been the pioneer in safety and has created unique and innovative 
features that some motoring brands could not even dream of doing by themselves. 
For example, Brake Assist Plus – an optional system designed to adjust brake 
pressure based on pedal speed and distance between the car and other objects. 
This feature is an evolution of similar system found in other S-Class models and 
uses the vehicle’s radar, in addition to the usual Brake Assist’s monitoring of 
pedal actuation speed, to apply power brake boost as required. This requires the 
optional adaptive cruise control’s radar, so in vehicles not equipped with this 
feature standard Brake Assist is substituted.<br>
<br>
Pre-Safe: The W220 S-Class is fitted with an advanced Pre-Safe system, which 
uses vehicle-dynamic sensors to predict collisions by taking into account 
closing speed as measured by the vehicle’s radar. In the likely scenario of a 
collusion, Pre-Safe closes all windows and the sunroof, moves all seats and head 
restraints to a safer position, and tightens the seatbelts.<br>
<br>
Besides these, there is also Night View Assist – an optional feature that uses 
two near-infrared illuminators mounted in the headlamp clusters and a sensor in 
the windshield. The infrared beams are picked up by the sensor and an image is 
displayed in the instrument cluster. Mercedes-Benz claims that this system 
provides the same or more viewable distance as by the high-beam headlights, 
without blinding oncoming motorists. These features, however, are optional and 
some are available only in the high-end models.</font><p align="justify" style="margin: 3px 10px">&nbsp;<table border="0" cellpadding="0" style="border-collapse: collapse" width="100%">
