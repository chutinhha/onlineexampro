<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<br>
The ride quality of the S-Class is superb with beautiful cornering and the 
suspension working in perfect harmony with the road. Though the car has its 
inherent German stiffness, the suspension doesn’t reflect this fact till one 
pays a close attention, as it features the Airmatic DC that uses air springs and 
adaptive electronic to produce a firm or soft ride. The air suspension is only 
available for the smaller engines up to S500. The S600 and S65 AMG are equipped 
with an active suspension system called Active Body Control, which is based on 
steel springs.<br>
<br>
The interior has gone through significant changes but still seems to resemble 
its predecessor. The interior features a spread of buttons and gadgets with a 
beautiful analog clock between the centre AC vents. The most important change 
that has taken place in the interior is addition of the silver control wheel for 
the COMAND system.<br>
<br>
Since its origin, the S-Class has always represented the best the Mercerdes 
brand has to offer. Words like mature and elegant come out easily for this car, 
especially when one sees the development cycle it has gone through over half a 
century. No matter whether they borrow the rear end or the command console, the 
S-Class is more individualistic than all of its peers.
