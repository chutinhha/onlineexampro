<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">
An explorer at heart<br>
</font></b><font face="Verdana" size="1">
<b>Having spent close to 20 years in various countries, S Mohandas, Senior 
General Manager, National Heaters Industries Co LLC, doesn’t run short of 
options to unwind. They could range from exploring books to exploring the world, 
finds out P Aneel Kumar</b><br>
<br>
<br>
<img border="0" src="passtime.jpg" width="160" height="159" align="left" hspace="6" vspace="4">“Ask my wife. She will tell you how I unwind myself,” says S Mohandas. The 
Senior General Manager with National Heaters Industries Co LLC likes to describe 
himself as a family man. “Home is the best place you can unwind yourself. My 
children are grown up and they are out. So, my wife and I sit, chat and watch TV 
together,” he says.<br>
<br>
“I do read whenever I get the time. It takes a lot of effort to finish reading 
books at one go. Basically, I like non-fiction. Sometimes, the company 
recommends some books and we get to know about some others, then I pick them up 
for reading. Currently, I am reading Further Along The Road Less Travelled by M 
Scott Peck. This is certainly one of my favourite books. Whenever I go to India, 
I pick up lots of books. But, while I buy 10 books, I end up reading only one,” 
he says.<br>
<br>
He also enjoys watching sports events on TV. “I’m a passionate cricket watcher. 
Though it was painful watching India lose in the recent World Cup, I did enjoy 
the other matches. Of late, I’m following tennis, which again I’m very fond of. 
I’m not a sportsperson though,” he adds.<br>
<br>
Besides books and TV, he finds good company among his friends. “I have made a 
lot friends during my 20 months stay here. We keep meeting, discussing various 
issues over a meal. For that matter, socialising is big business here,” he 
believes.<br>
<br>
But, all this during the weekdays. What about the weekends? “We do go around 
places during the weekends. In fact, my wife closes the kitchen during the 
weekend. We experiment all kinds of food in Oman. I personally like Arabic food. 
I also like to plan every weekend, but my wife balances this by coming up with 
new proposals,” says the industry veteran, who has spent 4-5 years each in 
Bombay, Lagos, Nairobi, Colombo and Dubai. He likes Muscat though, for this city 
offers a combination of metro as well as village ambiance. <br>
<br>
His stay in various countries instigated him to explore new tourist locations, 
the most exotic one being Maldives. “I wish to visit Austria, which has been 
pending long in my list, but I might end up in my hometown Pondicherry,” says 
Mohandas. One thing, however, he doesn’t miss is visiting the church during the 
weekends. He also believes that Muscat is a very good place to drive, and one 
can enjoy seeing tourist locations like Jabal Akdar, Al Hotta caves, etc. 
“Though the entertainment quotient is very low here compared to Dubai, every 
person in his right mind should accept that the quality of life here is very 
high. One can really enjoy his/her stay in this place.”<br>
<br>
So, what will he do if nothing works for him on a day? “My wife and I can always 
fight over petty issues,” concludes the father of two grown up boys – the elder 
of whom has completed first year in medicine and the other has just finished 
high school. </font>
