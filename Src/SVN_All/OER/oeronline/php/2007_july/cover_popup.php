      <br>
      <b>
		<img border="0" src="adeaters.jpg" width="250" height="173" align="right" hspace="6" vspace="4"></b></font><font face="Verdana" size="1">Come October, Muscat will be the stage for a global phenomenon. The 
      internationally acclaimed event – Night of the AdEaters – celebrates its 
      25th anniversary with a show in Oman at the City Amphitheatre on October 
      24 and 25, 2007.<br>
      <br>
      The unique festive show promises non-stop action and showcases some of the 
      most humorous, artistic, compelling and creative international advertising 
      films that reflect the vitality and trends of world-wide advertising.<br>
      <br>
      Created in 1981 by Frenchman, Jean Marie Boursicot, the first Night, 
      Friandises, was held with two objectives in mind – to share Boursicot’s 
      passion for advertising and private library of ad films with the world and 
      to raise funds for restoration of historical films. Since then, AdEaters 
      has grown into a global phenomenon and the collection – representing 
      productions from 65 countries from 1898 to the present day – has more than 
      700,000 ad films adding additional 25,000 films annually.<br>
      <br>
      <img border="0" src="adeaters1.jpg" width="250" height="166" align="left" hspace="6" vspace="4">Now in its 25th edition, The Night of the AdEaters in Muscat will set the 
      stage on fire with two evenings full of the most amazing advertising films 
      in the world. The action packed interactive evenings will offer audiences 
      live entertainment with fabulous prizes and much more. The Night will also 
      be full of excitement and surprises for audiences, who can participate 
      unreservedly in the party-like atmosphere.<br>
      <br>
      The recent 4th June private pre-screening of the Night of the Adeaters at 
      the Al Shatti Cinema Hall, attended by Muscat’s corporate elite and young 
      trendsetters, saw a tremendous response. It was a memorable preview and 
      everyone is looking forward to being associated with the event in October. AdEaters Muscat 2007, being an extra special event on the occasion of its 
      silver jubilee, will be the first-of-its-kind event in Oman.<br>
      <br>
      <img border="0" src="adeaters2.jpg" width="276" height="176" align="right" hspace="6" vspace="4">The event offers a huge marketing opportunity for brands as it transcends 
      the barriers of age, culture and language. With a dual target audience 
      comprising the youth and the corporate sector, this high-quality and 
      high-visibility event provides a unique platform for brands that are 
      synonymous with the event’s image. Exciting road shows, promotions and 
      contests are being planned to kick off the AdEaters campaign, which will 
      include not only the corporate Oman but also the Sultanate’s youth, as 
      well as the diverse expatriate community. <br>
      <br>
      The coming months will see AdEaters’ fever gripping Muscat as the virus of 
      excitement and festivity spreads, building anticipation and commitment to 
      participate.<br>
