</font><b><font face="Verdana" size="4">
Making life easy for 
the exporters</font></b><font face="Verdana" size="1"><br>
<b>Nasir bin Issa Al-Ismaily, General Manager, Export Credit Guarantee Agency 
(SAOC), talks about export credit guarantees for non-oil exports from Oman</b><br>
<br>
<b>
<img border="0" src="cover6.jpg" width="190" height="227" align="right" hspace="6" vspace="4">How does the ECGA contribute in the promotion of Omani exports?</b><br>
ECGA marked completion of 15 years in 2006 since it commenced its export credit 
insurance operations. The Agency plays a catalytic role through its concerted 
efforts in promoting Omani non-oil exports by extending credit insurance and 
financial services to the exporters. ECGA meets the needs and expectations of 
Omani exporters by providing a range of export facilities, including export 
credit insurance that covers the risk of non-payment due to commercial and 
non-commercial (political) risks, post-shipment financing of exports through 
bills discounting with commercial banks as well as pre-shipment financing for 
working capital needs and for the last three years extending domestic credit 
insurance support to credit insured exporters.<br>
<br>
<b>How was the performance of ECGA in 2006?</b><br>
ECGA performed credibly, as the total credit limits issued in 2006 stood at 
RO208.2 million compared to RO178.7 million in 2005, a growth rate of almost 
16.5 per cent. In 2006, we covered exports to 4,113 buyers in 97 countries, 
compared to 3,825 buyers in 95 countries in 2005. It is very encouraging that 
Omani non-oil exports excluding re-exports have gone up by a whopping 46.3 per 
cent in 2006. Also for the first time, the level of the Omani produced non-oil 
exports exceeded that of re-exports. ECGA has been one of the driving factors 
for Omani exporters to venture out by trading with new buyers and enter 
non-traditional markets knowing that the credit risk is mitigated by ECGA’s 
protection against non payment risks from the buyers.<br>
<br>
<b>How is your credit risk distributed among different markets?<br>
</b>In 2006, the five GCC countries had a share of 60.9 per cent of our total 
exposure, with credit limits totalling RO126.8 million. Among them, the UAE 
market had more than 33.8 per cent of insured sales followed by Saudi Arabia 
(13.7 per cent), Kuwait (5.6 per cent), Qatar (4.7 per cent), and Bahrain (3.1 
per cent). Apart from the GCC countries, the largest credit limits issued to 
businessmen were for trading with the OECD countries (16.1 per cent) followed by 
other Asian countries (10.9 per cent), Middle East countries (10.1 per cent) and 
African countries (2 per cent).<br>
<br>
<b>How has been the growth of numbers of exporters that avail your services?<br>
</b>In 1991, we had four exporters using our services. By 2006, the figure had gone 
up to 163. This is encouraging considering the level of Omani non-oil exports 
have significantly grown through the years. <br>
<br>
<b>Can you tell us about the geographical spread of these exporters?</b><br>
The capital area has the lion’s share of such exporters at 79 per cent, followed 
by Al-Batinah 13 per cent, Salalah five per cent and Al-Dakhiliya, Al-Dhahirah 
and Al-Sharqiyah one per cent each. <br>
<br>
<b>Which industries are availing your credit insurance support?<br>
</b>The share of exporters in the consumer products sector in 2006 was 36.3 per 
cent, construction 13.8 per cent, fisheries 12.1 per cent, garments 5.4 per 
cent, agriculture 2.3 per cent and other industrial products 30.1 per cent.<br>
