<font size="2" color="#333333"><b>Real issues untouched<br>
					</b></font><br />


				The government has committed to hire 100,000 employees from 2011 to 2013. If you take an average of 37,000 job seekers per year, it touches 110,000 employees for three years. Out of these 110,000, almost 50 per cent has been appointed just for the sake of absorbing the numbers. If the salary of an entry level job in military is RO500 per month, it will be RO6000 per year for each employee; the salary for 50,000 people is RO300mn. Then you have to give additional increment and the annual increment. If you take all this into account for the next 20 years it goes to RO7.5bn.<br />
				<br />
				Rather than paying RO7.5bn in the government sector, the government could have appointed 100,000 in the private sector by absorbing RO150 per month out of the total RO350 minimum wages. The rest of the money could be paid by the private sector. The impact of this is RO180mn per year. For the next five years, suppose if they had absorbed this cost, it would be only RO900mn. But today, many people are reluctant to join the private sector because they are expecting a higher paying job in the government. If the government jobs would have not been available, it would have been much easier for the unemployed to accept RO350 from the private sector itself.
				

