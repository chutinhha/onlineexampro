<p style="margin:10px; " align="justify" style="margin: 10px" dir="ltr" class="style15">

				The Al Rafd Fund will bring together the existing funds, including ‘Sanad’ Fund for supporting the small and medium-sized enterprises, Livelihood Development Programme, supervised by the Ministry of Social Development, Fisheries Fund, under the Ministry of Agriculture and Fisheries and Youth Fund and the Fund for Rural Women.
				<br /><br />
				
These funds have been put under Al Rafd Fund, and ‘Sanad’ Programme will continue as it is but under the umbrella of the Al Rafd Fund, and will work within the programme of the new Al Rafd Fund. The fund, at the beginning, will target groups belonging to the social security, as well as for rural women, professionals and craftsmen, in addition to other categories, such as categories of entrepreneurs and small and medium-sized enterprises.
<br />
<br />
This fund will have a great link to an important decision taken by the symposium on the development of small and medium-sized enterprises. His Majesty the Sultan has approved  the establishment of an institution for SME development which will provide advice and financial, administrative and  technical support for the small and medium sized enterprises and this fund will be supporting these institutions.
