o	Programme for training the employees at the governmental sector who have direct contact with the private sector. To be launched during the second half of 2013.<br /><br />
					o Government employees who want to set up personal 
					enterprises on full-time basis will get salaries for one 
					year. Regulations to be approved during 2013.<br /><br />
o	An annual prize that targets the SMEs and their entities was instituted. The qward to come into force in 2014.<br /><br />
o	An independent governmental entity to be in charge of providing them sufficient financial and human resources.<br /><br />
o	Allocation of land in different governorates in the Sultanate to build incubators and business centres for the SMEs.<br /><br />
o	Allocation of 10 per cent of the value of the total purchases and pubic tenders to the SMEs. Tender Board to implement the procedures during the first half of 2013.<br /><br />
o	Contractors of major project to allocate at least 10 per cent of the value of their contracts to SMEs. Practical procedures shall be taken before the end of 2015.<br /><br />
o	Improvement and development of single window clearance at the Ministry of Commerce and Industry. All services electronically from the end of 2013.<br /><br />
o	Laws that regulate competition and combat monopoly and declaration of corruption to be revised. Proposed amendments will be introduced in 2014.<br /><br />
o	The owner and full time manager along with his family (wife and sons registered in social insurance) of an enterprise will be included in Omanisation rates.<br /><br />
o	A system by which all private enterprises will transfer the salaries of employees to their bank accounts. Implementation will start early 2014.<br /><br />
o	Credit facilities to SMEs through commercial banks to be increased.<br /><br />
o	Allocation of 5 per cent of commercial loans for SMEs.<br /><br />
o	Measures to be put in place to ensure facilitating the availing of loans in proportion with the size of these enterprises.<br /><br />
o	Oman Development Bank to facilitate loans to the SMEs and reduce mortgage guarantees: Restructuring to take place in 2013.<br /><br />
o	Development of direct capital sector, involved in SMEs during 2013: Executive plan to come into force during 2014. <br /><br />
o	Credit Information Bureau at Central Bank of Oman to include monitoring of credit status for enterprises and individuals. This will take place in 2014.<br /><br />
o	Mechanism to expedite the payment of installment for SMEs that have projects with different governmental organisations. This will be activated during the first half of 2013.<br /><br />
o	Conduct further studies on a package of interactive economic media programmes that promote entrepreneurship culture.<br /><br />
o	Launching of a business TV channel.<br /><br />
o	Entrepreneurship to be incorporated in student curricula and education programmes in schools and higher educational institutions.<br /><br />
o	Establishment of a centre for innovation under the umbrella of The Research Council (TRC) to be in charge of translating ideas and innovations into products and businesses.<br />

