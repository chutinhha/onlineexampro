					<b>
					HE Salah al Ma’awali, the director general of SMEs Development at the Ministry of Commerce and Industry points out that the decision to raise the minimum wages for Omanis to RO325 will not affect the SMEs that have limited number of staff as compared to major companies. The initiative will have a positive impact on the morale of workers and enhance their productivity, he tells Khalfan al Rahbi, editor-in-chief, Alam al Iktisaad Wal Amal. Excerpts from the interview:<br /><br />
					Don’t you believe that the decision will affect the SMEs especially as it comes on the heels of the SMEs symposium which discussed the challenges facing the sector?
					</b>
					<br />
					The decision is good as it will support the efforts made to make the private sector attractive for the Omani workforce. It will contribute to the stability of the manpower in the sector. The decision will not affect the small enterprises because the number of workers there is less compared to major companies. We encourage these enterprises to manage their affairs and involve members of the family and friends. This will help in making them owners and businessmen. On the other hand, they need not pay big amounts of money as salaries. We should also take into account that when you give the employees good salary, the productivity and loyalty to the company will increase. 
					<br />
					<br />
<b>					How can we alleviate the impact on SMEs?
</b>					
<br />

Many measures were taken to alleviate the impact on SMEs. Dedication of 10 per cent of the projects offered by the Tender Board to SMES and granting 10 per cent of the contracts of the major companies to SMEs are just few examples of the support provided to this sector. The SMEs should do their best to draw benefits from the support provided to them. SMEs may also get finance from the available funding resources which are well poised to help the sector. 
<br />
<br />

<b>Do you feel that the timing of the decisions is appropriate?</b>
<br />
Many studies were conducted and meeting among stakeholders were held before taking this decision. There is a grace period as the decision will be implemented from July 2013. We believe that this grace period is enough for the enterprises to settle their conditions and adjust themselves with the effects of the decision.
<br /><br />

<b>Have you received any response from SMEs after the decision was made?</b>
<br />
Yes, we have received many responses expressing their unhappiness over the decision. I believe that these responses were natural as they will affect the profit margins. On the other hand, workers were happy with the decision. To address the issue we will provide SMEs with some incentives. 
In general, I believe that the decision will have positive impact on the performance of these enterprises as it will raise the purchasing power of workers as well as their spending power. To have a clear picture, we should think of the pros and cons.

<br />
