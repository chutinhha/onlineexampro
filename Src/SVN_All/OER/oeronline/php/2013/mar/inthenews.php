<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">&nbsp;Nawras has successfully completed the first phase of its extensive network turbocharging programme, transforming the broadband experience from Al Bustan to beyond Musannah and introducing the latest 4G technology in Muscat.
<br /><br />
Sharing the details of the way customers can now enjoy seamlessly surf the internet, Ross Cormack, CEO of Nawras, said, “We are delighted to be giving this great news to our customers today. As we promised at the start of this programme in June 2012, we are now offering fast mobile broadband speeds as well as increased coverage and capacity in most areas of Muscat and Al Batinah South.
“Everyone can now enjoy better and faster broadband speeds for the same price and from today, those who want a superfast service can choose 4G. With the introduction of 4G services Nawras becomes one of the earliest operators in the region to adopt this technology.”
<br /><br />
Wolfgang Wemhoff, chief technology officer, outlined the huge scale of the Turbocharging programme which has led to the Nawras Network being upgraded from end to end. Starting from Al Bustan to beyond Musannah, Nawras has added 3G+ services to every base station, added new base stations and more than doubled the amount of broadband data traffic that can be carried at any one time. The launch of 4G services in Muscat adds an additional service with even faster speeds for those who require huge quantities of data.

<br />
<br />
Musab Nasser Said Al Haddabi, Nawras senior proposition manager – mobile broadband, showed the choice customers have with existing 3G+ prepaid plans giving fast download speeds of up to 1.5 Mbps. Beginning with the hugely popular daily 1 GB of fast broadband for just RO1, he gave other examples of a RO5 weekly plan with 3GB data and a 2GB monthly plan for RO9. Customers using this service now automatically get faster speeds and a better experience without having to change their plans.
Cormack concluded the presentation by announcing, “Today we are thrilled to tell our customers that life got even faster with the launch of our superfast 4G mobile broadband service which is available across the capital area with speeds of up to 15 Mbps. This service can either be used by customers with a 4G USB modem in a laptop or with a mobile WiFi which creates a superfast broadband hotspot for as many as 10 people at the same time. You can turn the whole family’s laptops, tablets and smartphones into superfast 4G broadband devices.”

<br />
<br />

Prepaid and post-paid price plans for the new superfast 4G service start with downloads speeds of up to 10 Mbps with 7GB of data for just RO24per month or 15GB of data for RO34. Nawras Ajel post-paid customers also have the option of a richer data plan with 30GB and a faster download speed of up to 15 Mbps, for only RO49.&#8194;
