The mission is to create an environment for green computing and ensure responsible use of energy and other resources by working with the ITA staff and harnessing knowledge and support from the community. The main objectives of the green initiative are as follows: commitment to ensure green IT implementation by creating a dedicated team to formulate the direction and measure its progress; launching initiatives to reduce, reuse and recycle and encourage such projects in partnership with the community; organising awareness sessions on best practices in green-IT and measure the impact of such sessions; and playing active role in the exchange of knowledge in green initiatives both locally and globally.
<br /><br />As part of the programme, a comprehensive green-IT strategic plan was set up, approved and backed by the senior management team of the ITA. Various project teams (compliance, event management and awareness) were formed  to implement the plan. Some of the major highlights of the project include incorporation of best practices within the Green IT industry into the health, safety and environment policy of the ITA and close monitoring of its implementation; use of environmentally safe products and equipment; encouraging recycling and adopting responsible IT practices; promoting the use of e-services and electronic media; use of intranet sharepoint portal for communications; use of online forms, surveys and feedback that enable instant compilation to improve integrity and promote transparency; and creating awareness about an eco-friendly life style etc.<br /><br />
In addition, a comprehensive organisation-wide ‘Responsible Computing’ strategy was put in place backed up by health safety and environment policies that translate the strategy into action with full support from the top-management.
The initiative was instrumental in changing office culture, motivating other private and government organisations to launch a similar green programme and creating wider awareness through hands-on practices  such as Green Initiative for kids during Comex 2012.&#8194;
