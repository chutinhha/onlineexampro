<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">With the demise of Sheikh Essa bin Mohammed Al Zedjali, arguably the doyen of Oman’s media industry, the Sultanate has lost one of its illustrious visionaries who left behind an unprecedented legacy both as the founder of the first English daily in the country and as a gifted and prolific writer whose insightful articles were published in the leading newspapers across the region. OER pays tribute to this pioneer in Oman’s journalism firmament by republishing a detailed interview Mayank Singh had with him in 2010 when Times of Oman, his brainchild, celebrated its 35th anniversary<br /><br />
<b>Times of Oman (TOO) has completed 35 years in Oman. Can you give us an account of the initial years and the progress of the brand in the last three decades?</b>
<br /><br /><center><img src="images/in_remebrance_1.jpg" width="210" height="320" border="0" align="absmiddle"><br /><br /><i>The first edition of Times of Oman
dated February 23, 1975</i></center><br /><br />We started TOO on February 23, 1975 as I felt that the country needed a newspaper in English. In those days there was no radio, television station or newspaper in English, so this became a limitation in showcasing the country to outsiders as there was no one there to speak on your behalf. It was difficult to set up the first English newspaper but since I owned a printing press – the Oriental Printing Press, it was relatively easier for us to overcome the teething problems. TOO started out as a weekly tabloid with only 16 pages. The biggest challenge was to get editors and reporters who were proficient at English as there were no English speaking Omani journalists. As a result we had to get expatriate journalists from India, Philippines or Pakistan.
The idea for the newspaper came when I was working for the Ministry of Foreign Affairs (MFA). I joined the MFA on February 22, 1972 as director of five departments – finance, administration, consular, protocol, security and coordination – as in those days there were few educated Omanis. The MFA had a staff strength of 16 people and all my colleagues were looking after two-to-three departments. One April 1, 1974 I received a telex from a Chicago-based company called Flying Tires. Its Singapore office had sent in a request seeking permission for a fly over. The telex was addressed to the Ministry of Foreign Affairs, Muscat, Sultanate of Oman, Saudi Arabia. I lost my temper on seeing the telex and wanted to destroy it, but I kept my cool. I kept seething for three days on the thought of an independent country like Oman being mentioned as Saudi Arabia. After three days I realised that Oman had few consulate offices abroad and there were no English newspaper, television or radio stations to educate people abroad. I gave the company permission for the fly over with a warning to address their future correspondence correctly to the Sultanate of Oman. Simultaneously, I decided that we needed to start an English newspaper to educate people about the country.
<br /><br />
<b>TOO has been a leader in the English newspaper category for decades, what accounts for its success?</b>
<br /><br />
Our readers have been our biggest strength. They have appreciated our editorial content, layouts and design. We give both local and international news. Around 65 per cent of our readers are Indians and we take care to provide them with relevant news. I am happy with what we have achieved, but we have a more in-depth vision. We need to constantly improve the newspaper to provide more value to our readers and that requires constant effort from our side.
<br /><br />
<b>In a business-driven environment, how has the newspaper maintained a balance between editorial content and marketing pressures?</b>
<br /><br />

We have separate editorial and marketing teams. In addition there are advisors. These teams function independently but also coordinate with each other. There is harmony and coordination between our various departments which help in striking the right balance. Secondly, we depend on reputed news agencies which help us to maintain impartiality.
<br /><br />
<b>How has the media business changed over these 35 years?</b>
<br />
<br />

Things have changed drastically over the years. The industry has matured and progressed. For example, people have started reading newspapers in an electronic form. And we too have moved in this direction. Muscat Press and Publishing House (MPPH) has its own in-house IT development team, which takes care of all our IT related developments. We have launched first-of-its-kind products in the Middle East, starting from WAP services, e-mobile newspapers that enable readers to see the entire newspaper on their mobile phone. TOO and Al Shabiba both are available as e-mobile newspapers. We have mobile services which provides entertainment content – except for mobile operators, no one has this service in Oman. We have launched an electronic newspaper where you can see the entire newspaper as a soft copy. MPPH is the first media house in the Sultanate to tie up with NewspaperDirect – the digital worldwide network. The tie-up makes TOO and Al Shabiba available across the world in all Intercontinental hotels. We have launched i-phone applications, another first in the Middle East. On the marketing side clients can book a classified ad in our publications using a mobile phone. People staying in the interiors like Ibri, Sohar, Salalah (and Muscat too) etc can book a classified advertisement in our newspapers by just sending us a message and the ad gets published in the next day’s newspaper. 
<br />
<br />

<b>You have been the Editor-in-Chief of Times of Oman since its inception. Can you give us details of your achievements and what is your message for young journalists?</b>

<br /><br />

Starting with one newspaper we have grown the number of titles that we have over the years. Today we have TOO, Al Shabiba – an Arabic newspaper, Thursday – a weekly magazine, Hi! a weekly newspaper which comes every Friday; Al Youm Al Sabe, Black and White, a fortnightly magazine and Faces. Overall, we have a 65 per cent market share in Oman. We are proud of our newspaper, our achievements and our work. We have a strong editorial, marketing and IT department. Starting with 13 people in 1975, we now have a staff strength of 300. We have two printing presses, out of which one remains on standby for any contingency. For every ten rials that we earn we spend nine rials on developing our people, as they are our key asset.
My advice to young journalists is that they should devote themselves to studying journalism in English. Since the Sultan Qaboos University does not offer journalism courses in English, I would suggest the private sector to establish a college or University for English journalism. This will encourage Omani journalists to get educated and trained in English journalism. The industry today, faces a shortage of Omani English journalists and as we go ahead we need more Omani journalists.

