<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">The company previously known as Al-Seeb Technical Establishment (SARCO) was established in Oman in 1976 as a partnership between Juma Al Majid from the UAE and Late Abdulla Moosa Abdul Rahman. SARCO was then dealing with air conditioning, refrigerators etc and later diversified into electronics and white goods, lighting and the furniture business.
<br />
<br />
The manufacturing and contracting arm came into formal existence in the year 1992 and the name was changed to Shamel Industries Trading and Contracting (Shamel).  It also set up a purpose built factory spread over an area of 80,000 square metres, in the industrial suburb of Ghala in Muscat. Says P Sasidharan, CEO, “When I joined the company in 1994, the manufacturing arm of the organisation had 10-12 people. Then we started marketing the company in earnest and taking on jobs like villas, apartments and so on. But our real growth started when we got registered with the Diwan of Royal Court Affairs, which is currently known as Royal Estate.”
<br /><br />
Registering with the Diwan of Royal Court Affairs proved to be beneficial for the company in other ways too. As ‘the highest authority, demanding the highest quality’, they trained and guided the company to improve its quality standards. “The Royal office and other departments also gave us a lot of support and we are proud of being associated with projects like the Royal Opera House, Muscat, Majlis Oman etc” says Sasidharan. Another prestigious joinery project that Shamel is currently working on is the Oxford Islamic Centre, <b>UK</b>.
<br /><br />
</font>
<font face="Verdana" size="2"><b>Spreading its wings </b> </font>
<br /><br />
<font face="Verdana" style="font-size: 8pt">
Having made a mark for itself in Oman, Shamel ventured out of the country. It’s first foray outside the Sultanate was in Qatar, when it won a contract for doing the entire furnishing of a three star hotel. The company has not looked back since then and it has gone onto win bigger contracts such as the Grand Hyatt in Dubai; the Palace in Ras Al Khaimah; Hilton, Ras Al Khaimah and Atlantis, Dubai.
“We are proud that we have been associated with the Atlantis, where we fitted out the entire public areas. The work has earned high appreciation from our clients,” says Sasidharan. The company’s reputation has spread as a result of the positive word of mouth that it’s work generates. “We are not heavy on advertising, but the consultants and designers who deal with us recommend us to other clients and that’s the way our standing has grown in the region.”
Shamel has also ventured into India, where it has done a part of the refurbishment of JW Marriott in Juhu, Mumbai. Having worked on major projects, it has acquired a high level of specialisation in fit-out and management. The company was invited to participate in the fit-out work for Mumbai International airport, which is being carried out now. Familiar with the company’s work, MACE, the consultants for the Mumbai airport pre-qualified Shamel for doing the fit-out and project management of the airport.
<br /><br />Says Sasidharan, “We have been working at the Mumbai airport for about a year and have proved ourselves. As a company, we have acquired expertise in this field and are looking at spreading to airports in other parts of the world. We can do a similar job in the airports in Oman, if the opportunity presents itself, but so far we have not been successful.” <br /><br />Shamel is also expecting to be awarded a job for the Bangalore airport shortly. “In India we are appreciated for our HSE standards, planning, engineering, design and execution,” adds Sasidharan.
<font face="Verdana" size="2"><b>Raising quality standards</b> </font>
<br /><br />
Shamel counts on the ability to do a quality job at a reasonable price and meeting the requirements of clients as its biggest strength. It has also established in-house competence by establishing a joinery and furniture factory with departments for special carving, soft furnishing, upholstery, and factories for gypsum, GRC & GRP. Shamel also does special decorative finishes like gold leafing, stippling etc.
<br />
<br />
Sasidharan who has worked with Shamel for 18 years, has been instrumental in putting in place various systems and procedures in the company. “We have always ensured that the company grows by satisfying our clients and taking care of the stakeholders. If there is no sustainability of the business it will not survive in the long term. One has to be honest with its clients. We get a lot of repeat business; so far no client has walked away without being satisfied. There have been times when we are not able to service our clients and we tell them the truth.”
<br />
<br />
Aware of the need to diversify, the company has started focusing on project management and consultancy in the last couple of years. As part of the effort, it has recruited managers with a specialisation in these verticals. In addition, it is also looking at intensifying its Omanisation levels, by recruiting skilled nationals, though this remains a challenge. “Presently there are few qualified Omanis to take up these jobs as it requires special kind of skill level. With the government policy and effort, we are trying our best to bring in Omanis and to train them, but retaining them is an issue,” adds Sasidharan.
<br />
<br />
Sasidharan signs off by saying, “Our side of the business has not been growing as a vertical take-off but it has been growing gradually at 10-12 per cent. I am confident about the future as Oman has a robust economic plan and the country is growing accordingly. I don’t see any problem for us in the short term for another five years, but within these years the company should be ready to change, diversify and reach other markets and we are in the process of doing that. One has to thank His Majesty’s vision and wisdom in guiding the country to the enviable progress we all enjoy now”. 
<br />
<br />
Having blazed a trail over the last few decades, Shamel International is sure to continue coasting towards a brighter future.&#8194;
