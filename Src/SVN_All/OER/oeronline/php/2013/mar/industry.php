				<b><i><font face="Verdana" size="2">We met P Sasidharan, CEO of Shamel International, one of the best interior fit-out companies in Oman, at OER CEO Golf 2013. Sasidharan narrated briefly the history of the company and his vision for the future. An OER report</font></i></b></p>
				<p align="center" style="margin: 10px; line-height:17px"> 
