<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">

<br />The 2013 Index of Economic Freedom continued the practice of assigning the customary unfair ranking of most Gulf Cooperation Council (GCC) countries. As proof, Bahrain is granted a ranking position considerably ahead of Qatar, the UAE, Oman, Kuwait and Saudi Arabia. The survey assigns ranking numbers of 12, 27 and 28 to Bahrain, Qatar and the UAE respectively. In reality, these are the best results for GCC countries amongst 174 nations ranked in the index. Happily, these three GCC economies are properly grouped in the mostly free category.
Yet Oman, Kuwait and Saudi Arabia are granted ranking numbers of 45, 66 and 82 respectively, and grouped in the moderately free category of countries. The excessively low ranking for Saudi Arabia could not be justified for a nation known for having exceptionally active private sector investors, something put on display during the annual Hajj.
<br /><br />The gap of ranking amongst Bahrain, Qatar and the UAE is not justifiable, as the three countries share positions on numerous variables. This is particularly true of fiscal and monetary policies reflecting near absence of taxation. To be sure, Bahrain has maintained its worldwide ranking number 12 notwithstanding ensuing socioeconomic problems caused by an on-going political crisis. Amongst others, the development allowed for stronger governmental involvement, ostensibly to make up for hesitancy on the part of some private sector investors.
Amazingly enough, when it comes to economic freedom, Bahrain is merely ranked behind Hong Kong, Singapore, Australia, New Zealand, Switzerland, Canada, Chile, Mauritius, Denmark, the US and Ireland. The fact of the matter is that the Index of Economic Freedom provides little useful information on the methodology of assigning grades for 10 variables used in developing the index.
The 10 variables are grouped into four categories of open markets, regulatory efficiency, limited government and rule of law. The variables deal with trade, investment, financial activities, business, labour, monetary and fiscal freedom as well as government spending, property rights and freedom from corruption.
Another shortcoming relates to relying on results of other studies or secondary sources designed for other purposes. Arguably, absence of a primary study represents a major shortcoming of the index.
<br /><br />On a positive note, the report shows growing appreciation of economic freedom in the UAE. In fact, total marks assigned to the UAE grew by 1.8 points, enough to improve its ranking by seven notches.<br /><br />
The Heritage Foundation and the Wall Street Journal, noted for their conservative ideologies and embracement of private sector initiatives, stand behind the annual report. Understandably, the report regards governmental involvement as detrimental to proper functioning of the economy through causing displacement of scare resources.


<br />

<br /><b>Contradictions</b>
<br /><br />Certainly, the public sector can cause a crowding out of private sector investors when competing for facilities such as loans offered by financial institutions. For instance, this could cause hike in interest rates, with all the adverse effects to businesses and consumers. Conversely, findings of the report contradict other indexes, notably the Global Competitiveness Report 2012-2013, published by the World Economic Forum.<br /><br />The competitiveness report assigns ranking number 35 to Bahrain, ahead only of Kuwait, certainly a marked contrast to that of 2013 Index of Economic Freedom. The competitive index ranks economies on the basis of their achievement on numerous variables including labour market efficiency, financial market sophistication, innovation, macroeconomic stability and infrastructure.
Nevertheless, the significance of the Index of Economic Freedom cannot be overlooked partly reflecting its linkage to the Wall Street Journal. Also, it stands out for being one of the first surveys published after the New Year.&#8194;
