				<b>&#8207;The Kia Quoris strikes an enviable balance between refinement, features, drive quality and attractive pricing. An OER review </b></font><p align="center" style="margin: 10px; ">
				<img border="0" src="images/autotalk_1.jpg" width="500" height="200"><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
			&#8207;The all-new flagship contender from Kia -- the Quoris, surprises one with its built, refinement, features and driving quality. What does Quoris mean? Well, it’s supposed to convey solidity, luxury, safety and high-technology. And Kia’s first rear-wheel drive luxury saloon, offers all of that and more. Over the years Kia has undergone an impressive transformation, positioning itself as a leader in several categories. The new Kia Quoris is the most recent revelation that will leave delighted customers with heightened brand appreciation and increased demand. 
				<br>
				<br>
				<b>&#8207;Key differentiators</b>
				</font><font face="Verdana" style="font-size: 9pt">
				</font><font face="Verdana" size="1">
			The car’s overriding USP is that it packs in features of higher segment cars at a much lower price. It comes with a 3.8-litre, V6 engine putting out 294bhp through an eight-speed automatic. The car has an even weight distribution, a stiff body shell and is loaded with technology and gadgets.
			<br>
				<br>
				There’s a 12.3-inch LCD instrument panel, haptic feedback steering-wheel mounted controls, separate rear climate controls, two 9.2-inch screens for rear entertainment and a 17-speaker sound system. There are adaptive lights, collision warning radars, cameras, adaptive cruise control with full all-round view, head-up display, air suspension on 18-inch alloys. There is even a button to check the direction of Qiblah for prayers.
				<br />
				<br />
				<strong>&#8207;True performer </strong>
               <br />
               On the road, the Quoris is a competent highway cruiser, with the option to increase ground clearance. The driving experience is comfortable and comparable to some of the best vehicles in its class. The sedan offers three drive modes: Normal, Sport and Eco. The Normal mode is perfect for city driving while one can switch to Sport while cruising on the expressway. The eco mode offers fuel efficiency. The Quoris will definitely appeal to those seeking a smooth, executive ride.
               <br />
               <br />
               What surprises – and pleases – about the Quoris is the interior technology. The full option Quoris has more features than a comparable Mercedes, BMW or Audi in this price range. The cooling seats are a blessing for the Middle East heat. Video screens on the back of front seats provide entertainment, complete with radio options, alongside the impressive 17 speaker sound system.
               <br />
               <br />
               Apart from the features, it also matches its rivals in terms of the quality of the finish. The front of the car is stylish and impressive, with the front grille betraying shades of the marquees trademark tiger grill. The LED headlamps add a dash of style to the front.  For the price, the Quoris does offer a range of interior technology and this is the core selling point of the Quoris – more options for less money. Overall, the Quoris offers an alternative to those who seek comfort and luxury at a bargain.&#8194;
