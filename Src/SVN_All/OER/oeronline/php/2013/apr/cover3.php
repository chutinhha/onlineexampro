		        <td width="100%" valign="top"> 

                    <hr color="#C0C0C0" size="1">
<p class="style15" style="line-height: 14px">
It was only a couple of months ago that Bank Nizwa started its commercial operation as the first full-fledged Islamic bank in the Sultanate.  How is the initial response from the market?  
The response has been very enthusiastic as we expected.  We receive a lot of people who come to inquire about our services and open accounts with us; and they are very much impressed by the manner and the promptness and speed with which we can open an account for them.  We promise that if you are eligible we can open your account, get your check book and ATM card before you leave the branch.  But as the number of the visitors has increased steadily, and as those who come with a volley of questions about our services and facilities have required more time, we have arranged some of our staff exclusively to handle such queries, so that others can complete the process without any delay.   

<br />
<br />
<b>What are the Islamic banking products and services that Bank Nizwa offers right now?<br /> And what sharia contracts/tools does each of them apply?  </b>
<br />
<br />
Basically, people expect us, as a bank, to offer current accounts and saving accounts; and many of them want it along with additional facilities.  Today we already offer these services, and we allow our current account holders to use the ATMs of all other banks in the country at our expense.  It means all ATMs in the Sultanate are ipso facto Bank Nizwa’s ATMs.  As a new bank, we cannot set up ATMs across the length and breadth of the country overnight and therefore we had to find out a mechanism to serve all people who bank with us.  For instance, people who live 500 kilometres away from Muscat cannot wait to open an account with Bank Nizwa till we set up an ATM machine there.  We also offer saving accounts, personal finance, auto finance and home finance.  

<br />
<br /> 
In home finance, the sharia model we started with is Ijarah Muntahiyah Bittamlik (a sharia leasing contract whereby at the end of the contract the ownership is transferred from the lessor to the lessee). This is very practical and more effective at this stage. But sooner we are looking to introduce other facilities such Istisna where people can come with a design in their mind and ask us to build a house of their willing wherever they want, instead of buying an already built house.  We build it and lease it for them with an option to own it later. Istinsa is the Islamic tool for what you call project finance in the conventional stream.  We can also apply other models depending on the requirements of our clients in the future, whether it be setting up the headquarters for a company or building its warehouse or manufacturing facility.  Apart from Murabaha, Ijara and Istinsna, we will also be doing in the future equity participation, either through direct ownership or Mudharaba participation wherein the investors  who give us the money would be called Rabbul Mal (owner) while we will be the Mudharib (those who deploy money and invest it for them).    

<br />
<br />
<b>What about your ATM and branch network?  Where else do you want to start branches this year? 
</b><br />
<br />
Right now we have three branches located in Muscat, Sohar and Nizwa. But by the end of 2013 we plan to open other five branches, to end the year with eight branches across the country.  And as I mentioned earlier, all ATMs in the Sultanate will be part of our network.
You have a huge paid up capital of RO150mn. 
<br />
<br />
<b>What is your comment on the the recent CBO relaxation for Bank Nizwa in overseas investment ceiling?
</b><br />
<br />
The Central Bank of Oman allowed us to do that because there are not many Islamic instruments available in the Sultanate where we can invest our money. But the concession the CBO extended to us is only for a stipulated period and once it is over we will come under the normal regulation as any other bank in the country. It is obvious that the concession was on the basis of our request. 

<br />
<br />
Islamic finance is predicted to make rapid headway in the country with its asset base reaching RO1.5bn by the end of 2013.
<br />
<br />
<b></b>How prepared are you for this as you have to compete against numerous windows of conventional players who have already started operation? 
<br />
<br />It might be too early to talk about competition, actually. We have not run into each other so far.   There is enough room for everybody.  We kept saying form a practical point of view that we welcome competition because first of all it helps the clients. On the other hand, it helps us to create awareness rather than one company doing it alone.  And thirdly, we exchange with them products. Today if they have problem of liquidity, I can help them out; or tomorrow if we have the problem they can help us. The monopoly of one single bank does not work. With more players we will have a vibrant Islamic banking market. But still there needs to be a limit on how much competition you can bring to the Sultanate at this stage and there should not be an oversupply of institutions.  And I think the Central Bank of Oman is the best judge to decide when to stop giving licenses.

<br />
<br />
<b></b>Islamic finance is still in the fledgling state in Oman. What are the major challenges you confront at this stage?  
<br />
<br />
The biggest challenge is to create awareness among people about Islamic finance. People have to understand what Islamic finance is, rather than saying “I love therefore I want”.  They have to understand the importance of Islamic finance and what it can bring to the economy and that takes time. This is one of the biggest challenges because people might be confused by inaccurate information and descriptions presented by some for marketing purposes.  People want to understand the fundamental difference between the new and conventional banks.   There are many things that they still want to know. We are generating awareness through various types of forums, seminars and conferences. We are working with universities by delivering lectures and presentations in order to explain to the students who are going to be the future operators in the market. We need to reach out to more youngsters who constitute a huge chunk of Oman’s population.  We also make use of media as a channel to send our message across. It needs to be a collective effort between us, the policy makers, education institutions etc. to ensure that the message comes from more than one source.
The second challenge is reaching out to as many people and areas as possible in the Sultanate because everybody in Oman wants Islamic banking. But we have to walk before we run and therefore we gradually want to accommodate everybody aspiring to do business with us. We have to explore more innovative and creative ways to reach out to everybody. One solution is to open new branches but it will take time to open a physical branch by recruiting new employees as it involves getting them relieved from their previous employers and giving them proper training.  The third challenge is that people’s expectations are high and our ability to expand is controlled.  It is not as easy as being a bank and turning it into an Islamic one. 

<br />
<b><br />How do you manage to find experienced Islamic bankers? </b>
<br />
<br />
Although we have very good bankers in Oman, it is difficult to get experienced Islamic banking employees.  We are trying to bring Omanis who are working outside the country in Islamic banks such as Saudi Arabia, UAE, Bahrain and Qatar. In addition, all our employees have to go through an intensive training programme.  Even when we open a new branch we do training to the new comers for three weeks from 8am to 5 pm.  In addition, every month they undergo a refreshing course where we have our Sharia experts interacting with them. We also get our feedback from our call centre about the most frequented questions asked by people. We train our people to give everybody the same answer to the same questions.&#8194;
