The initial response from the market is remarkable. We were amazed with the positive response from the public and the high demand for sharia-compliant products. Since Muzn was launched, we have been receiving lots of customers opening accounts with Muzn and conducting Islamic transactions, be it placing of funds or financing requirements. We strongly believe that our market share of Islamic banking will be very high compared to our competitors, noting the high demand for the services and the products offered by Muzn which are by far the best in the market.

<br />
<br />
<b>What are the Islamic banking products and services Muzn offers right now?  And what sharia finance models they apply?  </b>
<br />
<br />
Muzn Islamic Banking caters for the retail and corporate requirements and our products cover the needs on both the assets and the liabilities sides, from Mudaraba accounts for retail saving accounts; Wakala for corporate and retail deposits; and Flexi wakala for call accounts. Also, the asset side includes Ijara for home finance, home construction finance and term financing; Murabaha for cars and assets financing as well as trade finance services.
<br />
<br />
<b>What about your ATM and branch network?  Where else in Oman do you want to open branches of Muzn this year? 
</b>

<br />
<br />
Currently we have one branch in Muscat. However, our plan is that we will cover the entire Oman by the end of 2013 as Muzn will open seven more branches in all the regions of Oman to cover customers’ needs. We are facing increasing customer demand from all the regions requesting for Muzn services which will drive us to speed up the process of opening more Muzn branches to fulfill our customers’ needs and expand our reach. Currently all Muzn customers can use the National Bank of Oman ATMs free of charge, which reflects Muzn’s commitments to its customers and community. 
<br />
<br />
<b>Islamic finance is predicted to achieve an asset base of RO1.5bn by the end of 2013.  How prepared are you for this as you have to compete against full-fledged Islamic banks with huge paid-up capital?
</b><br />
<br />
Muzn is the first Islamic banking window to open its doors to the public. It is also the first Islamic bank to register with the Ministry of Housing and the first to offer Ijara services to the customers, which gives a clear idea of how Muzn is well prepared. We are well ahead in the market with our dedicated and well trained Islamic banking team and our product suite which is geared up to cover all market requirements.

<br />
<br />
<b>Could Muzn leverage the National Bank of Oman’s existing brand image in the market to address new Islamic banking customers?
</b><br />
<br />
Muzn is supported by the National Bank of Oman from an operational perspective; being the parent company of Muzn, National Bank of Oman provides special treatment, services and rates to its customers to promote Islamic banking within the group. 
There were concerns about the feasibility of conventional banks offering Islamic services through window operations. 
<br />
<br />
<b>What are the measures you have taken to alleviate such concerns?  
</b><br />
<br />
Muzn is operating with the concept of an Islamic bank within a bank, with a separate balance sheet, separate paid-up capital to support its operations, separate IT system and separate core banking system. This will allow Muzn to offer its services in the market as a full-fledged Islamic bank. Our services with National Bank of Oman are mainly concentrated in the field of operational support. 
<br />
<br />
Muzn sharia supervisory board comprises three of the best scholars in the Islamic banking field in the world with extensive experience in this field. They are Sheikh Dr Mohamed bin Ali Al Qari, chairman of Muzn’s Sharia Supervisory Board (SSB) and board member in many Islamic organisations. He is also a sharia professor in king Abdulaziz University in Saudi Arabia as well as penned so many articles on Islamic banking. We also have on board Sheikh Dr Mohamed Daud Baker, chairman of the Central Sharia Advisory Council of the Central Bank of Malaysia, Securities Commission of Malaysia, and Labuan Financial Services Authority as well as many other Islamic organisations, and Sheikh Khalfan Al-Esry, member of Muzn SSB and a well-known Sharia scholar in Oman with solid experience and knowledge of Islamic banking.
