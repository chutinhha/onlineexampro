					<font color="#333333" size="2"><b>Poised for a quantum leap <br></b></font>
					<br />
				<center><img src="images/coverstory_15.jpg"><br />
				<font size="1" face="Verdana">Dr Jamil El Jaroudi, CEO, Bank Nizwa</font></center>
				<p class="style15" style="line-height: 14px">	<font size="1" face="Verdana">
			Despite being at its embryonic stage, Islamic finance is predicted to have the potential to redraw the banking landscape of the Sultanate in the years to come. Being the first full-fledged Islamic bank in the country, Bank Nizwa is apparently in the vanguard of Oman’s historic tryst with faith-based finance. Its CEO Dr Jamil El Jaroudi exudes confidence that the bank is geared up to assume this challenging responsibility, in an interview with Muhammed Nafie.</font></td>
