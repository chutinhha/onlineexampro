<b>How is the initial response to Meethaq?
</b><br />
<br />
The response has been quite exciting and the number of inquiries has been more than what we expected so that we are forced to double the number of the staff. There are two types of customers: those who come after making up their mind and those who initially come just to inquire and then come back to us after going to other banks and understanding what distinguishes our products and services.  

<br />
<br />
<b>What are the Islamic banking products and services Meethaq offers right now?  And what sharia finance models they apply?  </b>
<br />
<br />
For the time being we offer current account, savings accounts, home finance, and auto finance. We have also launched corporate financing and have already started financing a number of projects of which the biggest one accounts for about RO12mn. And a number of bigger projects are in the pipeline. In general, we apply three sharia contracts. For project finance, we primarily use Musharaka and Ijara while for auto finance we use Murabaha, as most of the banks do because it is easier. We are using Murabaha for savings accounts and Qardhul Hasan for current accounts. However, we have not yet started the more complicated contracts such as Istisna and Salam etc. We have a lot of products in the pipelines which will be out in a few months’ time.  
<br />
<br />
<b>Where else in Oman do you want to open branches of Meethaq this year?  </b>
<br />
<br />
We have planned to open seven branches this year. It looks a bit slower than we expected, but hopefully we will have seven branches in Muscat and other cities towards the end of this year. 
<br />
<br />
<b>Could Meethaq leverage Bank Muscat’s existing brand image in the market to address new Islamic banking customers? 
</b>
<br />
<br />
The Central Bank’s regulation requires that the window should remain as a bank within a bank, need to have a separate balance sheet and separate loss and profit account. We do a lot of awareness and education.  Recently, we trained about 200-300 customers and had a one week training forum in Nizwa. We were doing this even before we got the license.  We have explained to people that how different the Islamic banks are from the conventional banks and how a window can operate as a window and still be Islamic even though the parent bank is riba-based. Some of our existing customers are shifting to Meethaq. Earlier the existing customers did not have a choice; but now they have a choice of either staying with Bank Muscat or going to another Islamic bank. So they prefer to stay with Bank Muscat.  
<br />
<br />
We don’t hide our identity, contrary to some other Islamic windows who don’t use their parent company’s logos and where if you walk in it will be very difficult to make out their real identity-whether they are new banks or whether they are part of an established entity. But we are more transparent on this. Everybody knows that Meethaq is attached to Bank Muscat and a window of Bank Muscat, so why do we hide it? Bank Muscat is a very strong brand and if you are associated with a bigger brand, it’s good. 

<br />
<br />
<b>How prepared are you to compete against full-fledged Islamic banks, especially when there are concerns about the feasibility of conventional banks offering Islamic services through window operations?   </b>
<br />
<br />
The competition is intense. Now, every client you speak to, especially the corporate clients, are talking to other banks as well. After all, what matters to them is the pricing- the cost of financing. So it is very difficult. On one hand, the window operations can take advantage of the parent bank’s support functions such as HR, IT etc.  But on the other hand, this can also be very difficult because people with the conventional bank used to do banking in a given way and now they have to do in a different way. 
<br />
<br />
So we have got a lot of challenges in terms of convincing them that they are different and they have to be dealt differently. Money has to be segregated; there could not be the commingling of the funds. As far as full-fledged banks are concerned their entire operation is focused on one thing.  However we are giving training to them. Right now, five of my staff members are abroad undergoing on-job training in a bank in Bahrain to raise their knowledge, ability and confidence. So they can understand how to do it here in Oman.  They may be more skilled than the Bahrainis they work with, but we sent them to get more confidence. 

