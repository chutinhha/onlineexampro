Al Yusr is committed to its strict compliance to sharia rules and regulations. We have built our overall organisational structure in compliance with the Central Bank of Oman’s guidelines in this respect. We adhere to AAOIFI’s accounting, auditing and sharia standards. Al Yusr’s operations are being supervised by a competent and renowned sharia supervisory board. Dr Esam Al Enzi is the chairman of our sharia board. He is being assisted by Dr Khalid Al Siyabi and Dr Ahmad Al Ayadi.  Al Yusr also has a fully functional sharia control department. This department provides valuable guidance and consultation on the sharia issues to the bank’s management and is equally entrusted to closely monitor the routine operations of Al Yusr to ensure sharia compliance.
<br />
<br />
<b>Where do you want to open branches of Al Yusr this year in Oman? And what about your ATM networks?   
</b><br />
<br />
Al Yusr will have its dedicated Islamic banking branches. We will start our operations with five branches in the first year. These branches will be located in the cities of Muscat, Salalah, Nizwa, Sohar and Sur. All branches will have ATMs but our customers will also be able to use OAB’s ATMs as well as other banks’ ATMs. Al Yusr will bear the cost of OAB ATM network use as this is a sharia requirement. 
<br />
<br />
<b>Islamic finance is predicted to achieve an asset base of RO1.5bn by the end of 2013. How prepared are you for this as you have to compete against full-fledged Islamic banks with huge paid-up capital?  
</b><br />
<br />
This is an interesting question. We understand the potential in the market as well as the intense competition that will gradually emerge in the future. Al Yusr plans to focus on carefully selected market segments and our same strategy to focus on the niches drives the nature and design of our products. We intend to achieve the market leadership in our chosen areas. We do not wish to compete with full-fledged banks on product-by-product basis.
<br />
<br />
<b>Will Al Yusr leverage OAB’s existing brand image in the market to address new target Islamic banking customers? 
</b><br />
<br />
Oman Arab Bank enjoys the reputation of a solid, stable and committed bank. Indeed we wish to continue this legacy of prudent banking. Our overall Islamic banking strategy draws its strength from our time-tested business values. Our customers will experience the same benefits through Al Yusr that they have historically experienced with the additional assurance of sharia compliance. They will also have a thoroughly enhanced experience due to our recent technology investments for Al Yusr.
<br />
<br />
<b>There were concerns about the feasibility of conventional banks offering Islamic services through window operations. What are the measures you have taken to alleviate such concerns?  
</b><br />
<br />
Islamic banking windows have emerged as a successful business model in various countries like UAE, Bahrain, Pakistan, Malaysia, and Bangladesh. Window can easily tap to the existing clientele of the conventional bank, leverage on its relationships with corporates and regulators and rely on the existing operations, wherever allowed by the sharia.  Hence, in my opinion Islamic banking windows is a close alternative to full-fledged Islamic banks and, as it has been observed in other countries, can even grow to a size where they directly compete with full-fledged Islamic banks.
<br />
<br />
Al Yusr, in line with Central Bank of Oman’s directives, is about to launch its dedicated branches where only Islamic banking services will be offered to our customers. We have also acquired a sharia-compliant core banking system for Islamic set up. We have put in place a robust corporate governance sharia supervisory structure. Moreover, we have raised separate capital to run our Islamic business. Al Yusr also has a well-trained and experienced Islamic banking management team. All these initiatives make Al Yusr a fully sharia-compliant and will for sure remove any doubts from the minds of our customers.&#8194;

