<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">
synerggie Eventz invited Robin Sharma, the celebrated author of The Leader Who Had No Title, one of the bestselling business books on the market today, to speak on leadership and high performance in business recently at Ritz Carlton Al Bustan Palace Hotel. The event was part of  the efforts to develop the next generation of leaders in this period of intense change. 
<br />
<br />
Synerggie Eventz selected Sharma for his “Lead Without A Title” message, his strong track record as a CEO advisor and mentor to top global executives and their belief that regardless of position, leadership must be developed at all levels of an organisation in order to sustain competitive advantage.
<br /><br />
What makes an effective leader?  For Sharma, “Leadership 2.0” comes from an inner sense of who you really are. “The way you do the little things is the way you do everything,” Sharma advises. “Your performance on the outside is a reflection of what is going on inside.”
<br />
<br />
The theme of the seminar focused on the qualities that make some executives stand out as leaders regardless of their formal rank within a corporation. Bhaawana Devaraath, business head Synerggie Eventz captured the essential idea in her introduction, “People don’t follow titles,” she said, “They follow leaders.” Sharma’s philosophy is “making a difference and putting a dent in the universe.”
<br />
<br />
The event saw over 400 participants from various sectors and industries. The event was presented by The Zubair Corporation. Fluid Systems Corporation and Oman Airports Management Company were the strategic partners. Creative Links and Qalhat LNG were the associate partners. Knowledge Oman was the Knowledge and Communication partner. Times of Oman were the Newspaper partner. Oman Economic Review and AIWA were the business magazine partners for this event. Speaking on the occasion, Hamood Al Harthy, chief HR Officer for The Zubair Corporation said, “I have been very inspired to listen to Robin.” 
<br />
<br />
Much sought-after speaker Robin Sharma is the celebrated author of 11 bestselling books on leadership, including The Leader Who Had No Title (Simon & Schuster), and The Greatness Guide. His work has been published in over 60 countries and nearly 75 languages, making him one of the most widely read authors in the world. He shot to fame with “The Monk Who Sold His Ferrari” which has topped international bestseller lists and sold over 3,000,000 copies. Celebrity CEOs, rock stars, top entrepreneurs and royalty have all embraced his work. As well as being a much sought-after speaker globally, Robin is also a widely respected CEO advisor having worked with Fortune 500 CEOs and leaders all over the world.  Robin is the founder of Sharma Leadership International Inc., a training firm with only one focus: helping people in organizations Lead Without a Title. Clients include Microsoft, GE, Nike, BP, FedEx, and IBM. Organisations such as NASA, Yale University and The Young Presidents Organisation are also SLI clients. 
<br />
<br />
In a recent independent survey of 22,000 business people Robin was ranked in the Top 5 leadership experts (source: leadershipgurus.net). He frequently shares the stage with Jack Welch, Jeffery Immelt, and thought leaders such as Bill Clinton.
