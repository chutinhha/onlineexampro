						<font face="Verdana" size="4" color="#C00000">
						Riding on fundamentals</font></b><p class="style15">
				<span class="style14"><strong><font size="2">The OER-Gulf Baader Capital Markets survey of the Best Banks in Oman 2012 shows how &#8207;strong fundamentals and high hydrocarbon prices ensured a strong performance by the Sultanate’s banking sector in 2012  </font></strong></span><p class="style15" style="line-height: 14px">
						<p class="style15" style="line-height: 14px">
						<span class="style14"><font style="font-size: 8pt">
						
						Strong GDP growth and supportive monetary and fiscal policies had a favourable impact on business growth and performance of commercial banks in 2012. Oman’s banking system remained sound, resilient and profitable in 2012. Total assets of commercial banks increased by 15.3 per cent to RO20,681.4mn in November 2012 compared to RO17,934.2mn in November 2011. Total credit expanded by 16 per cent to RO14,333.6mn at the end of November 2012 from RO12,354.4mn a year ago. While credit to government declined by 8.2 per cent at the end of November 2012, credit to public enterprises and private sector increased by 15.6 per cent and 17.1 per cent, respectively.
						<br />
						
					<br />
					
				&#8207;Commercial banks’ overall investments in securities increased by 76.2 per cent to RO 2,202.1mn in November 2012 from RO 1,249.8mn a year ago. Of the total outstanding investments, commercial banks’ investments in CBO CDs stood higher at RO 1,022mn in November 2012. &#8207;Investments in Government development bonds (GDBs) increased by 52.2 per cent to RO 432.8mn in November 2012 compared to its level last year. Commercial banks’ investments in foreign securities increased to RO549.4mn in November 2012 from &#8207;RO223.1mn a year ago. On the liabilities side of the balance sheet, total deposits of commercial banks increased by 15.1 per cent to RO14,136.4mn in November 2012 from RO 12,282.7mn in November 2011.
				<br /><br />
				Private sector deposits with commercial banks increased by 13.3 per cent to RO 9,032.8mn in November 2012 from RO 7,974mn a year ago. Government deposits with commercial banks increased by 26.9 per cent to RO3,901.1mn, while deposits of public enterprises declined by 8.6 per cent to RO 979.9mn during the same period.
&#8207;The provisional net profits of commercial banks stood higher at RO 272.1mn at the end of November 2012 compared to RO235.5mn at the end of November 2011. Broad money (M2) increased by 11 per cent to RO 10,956.4mn in November 2012 compared to RO 9,874.8mn in November 2011.

<br />
<br />
Narrow money (M1), comprising currency held by the public and local currency demand deposits, expanded by 14.7 per cent to RO3,680.6mn at the end of November 2012 on a year-on-year basis. Quasi money (comprising RO savings and time deposits, certificates of deposit issued by commercial banks, margin deposits and foreign currency denominated deposits) increased by 9.1 per cent to RO 7,275.8mn in November 2012 compared to &#8207;RO6,666.1mn in the previous year.
<br />
<br />
As regards the sources of broad money supply, net foreign assets of the banking system including CBO increased by 10.9 per cent to RO6,125.8mn in November 2012 from RO 5,523.1mn in November 2011, while domestic assets rose by 11 per cent to RO4,830.6mn from RO4,351.7mn during the same period. &#8207;Omani banks are expected to report higher profits in 2013 and likely to grow more aggressively as they compete for market share, Fitch Ratings said in a report.
<br />
<br />
In its 2013 outlook for GCC banks, the ratings agency said that the outlook for Omani banks remained stable this year.
<br />
<br />
&#8207;“To date, Omani banks have achieved modest profitability due to a cautious growth strategy, but they are likely to grow more aggressively as they compete for market share now,” Fitch said. &#8207;According to Fitch, competition is likely to increase with the launch of Islamic banks and windows, which should generate some new business for both existing banks and new entrants.
<br />
<br />
&#8207;“Margins are likely to tighten due to increased competition, CBO-imposed interest rate caps on retail lending and slimmer margins on government-related business,” it said.
&#8207;The ratings agency said that the cost of funding was likely to increase in Oman due to competition for deposits and increased wholesale funding, unless this is offset by higher volumes of non-remunerated Islamic deposits. It added that fee incomes could improve, linked to increased trade finance and guarantee issuance.

<br />
<br />
Fitch said that asset-quality concerns, particularly relating to the domestic real-estate sector, still weighed on the viability ratings of Omani banks. &#8207;“Non-performing loans (NPL) ratios at some banks are relatively higher - although this has always been the case, reflecting write-off policies and significant interest in suspense - while concentration risk is also high in Oman due to the limited size of the corporate market. Household indebtedness is also significant, and measures have been introduced by the CBO to moderate this.”
<br />
<br />
It said that loan-to-deposit ratios are unlikely to improve in 2013, adding that banks were adequately capitalised and that it does not expect capital to be a concern in 2013.
&#8207;“Customer deposits would grow, but overall liquidity remains tight with the banks. Despite improving confidence about Oman, Fitch believes that access to international wholesale funding will be expensive, partly reflecting the modest ratings of Omani banks compared with other GCC banks,” Fitch added.

