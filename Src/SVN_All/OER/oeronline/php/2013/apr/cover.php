					<p class="style15" style="line-height: 14px"><b>
					<font size="2" color="#333333">&#8207;Future outlook<br>
					</font></b><p class="style15" style="line-height: 14px">
The outlook for Oman’s banking system remains stable, says Moody’s Investors Service in a February 2013 report titled ‘Banking System Outlook.’ The outlook is unchanged from 2007 and reflects (1) Oman’s supportive macroeconomic environment; (2) low and well-provisioned for non-performing loans (NPLs); and (3) ample liquidity buffers, underpinned by a stable deposit funding base. &#8207;These positive factors continue to be balanced by structural weaknesses related to the banks’ dependence on the small, undiversified Omani economy, which is heavily reliant on the hydrocarbon sector, and the presence of significant funding and borrower concentrations.
<br />
<br />
Moody’s expects Oman’s real GDP to expand by 4.1 per cent in 2013, fuelled by high oil prices and public spending, which will stimulate the non-oil economy and positively affect the banking sector over the 12-18 month outlook period. Oman’s stable macroeconomic conditions will create lending opportunities for banks and support credit growth of around 10 to 12 per cent in nominal terms in 2013. However, whilst Moody’s expects the operating environment to be supportive over the outlook period, the rating agency also recognises that Omani banks will remain reliant on the small local economy and the performance of the hydrocarbon sector (which accounted for over 50 per cent of GDP in 2012), leaving the banks vulnerable to the risk -- although unlikely -- of a sustained drop in oil prices.
<br />
<br />
The banks’ NPL levels will remain low, at around three per cent of gross loans over the outlook period. Moody’s bases this on (1) Oman’s stable macroeconomic environment; (2) improved labour market conditions, buoyed by government spending creating jobs; and (3) regulations introduced by the Central Bank of Oman during 2012, linking maximum retail loan amounts to salary levels. However, Moody’s expectations of solid asset-quality performance are counterbalanced by banks’ asset exposure to event risks, given their high single-party exposures, and a still-problematic real-estate sector where delinquencies remain well above the sector average.
<br />
<br />
According to Moody’s, the system will remain primarily deposit-funded, with customer deposits accounting for 86 per cent of the total funding or 72 per cent of assets as of September 2012, whilst the banks will continue to benefit from a high level of deposits from government and government-related entities. The system will also likely maintain sound liquidity buffers, with liquid assets -- defined as cash, interbank balances and liquid securities -- amounting to 26 per cent of total assets in September 2012. Nonetheless, Moody’s also expects that the high concentrations in the banks’ deposit bases will continue to leave the banks vulnerable to large deposit withdrawals over the next 12-18 months.
&#8207;With the proposed increase in spending by the government on infrastructure to boost opportunities for the banking sector, prospects for Omani growth are sound. GDP growth of around five per cent is expected for 2012, aided by increased hydrocarbon revenue and government sector is expected to grow to six per cent. &#8207;Major projects in progress in Oman include a rail network and new air and sea ports. In addition, the Sohar and Duqm industrial areas are expected to see a burst of activity in the petro- chemicals, oil-refining, metals and other industrial sectors as investments worth billions of dollars get underway.

<br />
<br />
The banking sector expects lending opportunities in the consumer segment to rise, given the substantial increase in salaries for public sector employees announced by the government since 2011 and new job creation for Omanis. Local banks rely heavily on revenues from consumer lending (capped by the central bank at 40 per cent of loans).
<br />
<br />
Unlike the central bank in the UAE, the Omani central bank has not interfered so far in setting maximum loan values (as salary multiples), debt to income ratios or maximum loan tenors. However, rising levels of personal indebtedness has prompted the central bank to encourage local banks to set more conservative lending criteria. This could reduce the consumer loan growth rates of local banks.
<br />
<br />
Omani banks have little exposure to the Eurozone. Moreover, with about 80 per cent of Oman’s oil-dominated exports going to Asia, the impact of the European crisis is limited as long as it does not lead to a significant fall in oil prices. However, the problems in the Eurozone have impacted the availability of overseas funding for the banking sector.
&#8207;Oman’s banking sector did well in 2012 in terms of asset growth due to higher lending to support increased economic activity, and the sector is poised for strong growth during 2013, thanks to the key emphasis on infrastructure development in the new budget.

<br />
<br />
Given the continuing high oil prices coupled with the positive macro-economic conditions, the overall credit off take in Oman’s banking sector is expected at be 10 to 12 per cent levels, amid robust Government projects spending in the pipeline and the anticipated recovery in private sector credit off take.&#8194;
