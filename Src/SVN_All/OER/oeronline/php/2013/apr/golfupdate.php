					<b><font face="Verdana" size="4" color="#C00000">Tough competition</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Assarain Classic Golf tournament 2013 was more entertaining and fun-packed with the newly completed back nine holes at the Ghala Valley Golf Course</font></b><p align="justify" style="margin: 3px 10px"> 
					&nbsp;<p align="justify" class="style13">
				<img border="0" src="images/gulf_1.jpg" width="132" height="263" align="right" vspace="6" hspace="6" style="margin-top:-5px;">
Over 100 golfers from the Sultanate participated in the keenly contested 18th Assarain Classic Golf tournament. The participants competed in the two-day, 36-hole stroke event in gross, net, lady and junior divisions. The event was held at Ghala Valley Golf Club and was open to all the golfers in Oman, with a registered USGA handicap. During the two days of the event the participants faced neck to neck competition and Yuri Gijselman emerged winner in Men gross division with 159 points followed by Jeff Campbell at 160 points and HE Mohammed Al Rumhy with 162 points.
Assarain Classic Golf tournament 2013 was more entertaining and fun packed with the newly completed back nine holes at the Ghala Valley Golf Course. Mubarak Hill, general manager, Ghala Valley Golf Club said, “Year upon year the Assarain Golf Classic has proven to be one of the hottest contested and enjoyable events on our calendar. This year was no exception with very tough competition among some of the best players from all over Oman. With the generous support from Assarain Group of companies we were able to put on a great show for participants and guests to enjoy both on and off the golf course.”

<br />
<br />
Commenting upon his win Yuri Gijselman said, “Assarain Golf Classic tournament is one of the oldest golf tournaments in the Sultanate and it feels great to win a competition of this stature. Each participant in the tournament was well experienced and was a tough competition. The match intensity helped me improve my game even further and I look forward to the other tournaments being held at the Ghala Valley.”   
<br />
<br />
In the Mens Nett division, Piet Loremans won with 141 points followed by Atul Kanwar with 142 and Ketan Metha with 145 points. Minke Gijselman won the Ladies Nett division with 146 points followed by Jacky Franssen with 147 and Marjika Van der lind with 149 points.  The ‘Nearest the Pin’ winner on day one was C Das, as day two winner was Yuri Gijselman. Day one straightest drive was won by Ans van Soest and day-two was Jeggi Jilan. Fahad Al Kitani won the Nett division and Hamood Al Harthy won the Gross division in the junior category. 
<br />
<br />
Commenting on the success of the event, Ibrahim Al Wahaibi, managing director of Assarain Group of Companies said, “I would like to congratulate all the winners in the respective categories. With the success of the 2013 event, we are committed to making the Assarain Golf Classic Tournament bigger and better in the coming years while maintaining our strong professional standards of hosting one of the oldest golf tournaments in the Sultanate.”&#8194;
