The brand “Stulz”of Germany, from KR ACD, completes the entire range of commercial requirements, as server rooms / Communication Exchanges are a regular feature in every big Office, requiring 24x7 cooling under highly controlled conditions of temperature & humidity. Stulz is one of the leading manufacturers of the world for Closed Control Units (CCU).
