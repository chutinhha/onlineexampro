						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">Pursuit of partnerships</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">TBWA\Zeenah and UK based Creative Action Design have entered into a partnership to provide spatial, brand and event design services in Oman.<br />
						Creative Action Design (CAD) is an internationally renowned design company which applies creative thinking, global experience and original approaches across a broad range of creative services incorporating commercial and retail spaces, branded environments and event stands. TBWA\Zeenah has tied up with the company to service its clients in the Sultanate. Mohammad Al Farei, managing director, TBWA\Zeenah and Ian Silverstein, founder, CAD talk to Mayank Singh about the tie-up. Excerpts
</font></b><p align="center" style="margin: 3px 10px"> 
					&nbsp;<p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/media_1.jpg" width="300" height="194">
					<p align="justify" class="style13">
<br />
<b>What is Creative Action Design’s differentiation in the market?
</b>
<br />
<b>Silverstein:</b> We specialise in working with international companies and help them in bringing their brands alive through various environments. This environment could be a retail premise, office environment, an exhibition stand or an event where a brand has a three dimensional presence and that is where our company has a specialisation. We have been attracted to Oman in the last three to four years as we have worked with both Nawras and Omantel and it has been a real pleasure working with these key brands. Essentially this partnership is about supporting and working with the credibility that TBWA has in this market and adding our skills and expertise alongside their existing team, complementing them and assisting them on current environment projects. 
<br />
<br />

<b>Has CAD had regional experience in the GCC over the last few years? 
</b><br />
<b>Silverstein:</b> We have been in the Middle East for 10 years, the first project that we completed was in 2002 in Bahrain, where we launched Vodafone. We then launched Vodafone in Kuwait. CAD ventured into Qatar in 2006 and we have had a presence in that market since 2006. We have a very active business in Qatar and that has been our main hub in the Middle East. I have been coming to Oman for the last five to six years and I have always been attracted first and foremost to the place, people and the business potential. We are really excited about the opportunity with TBWA.
<br />
<br />


<b>TBWA\Zeenah has had a working arrangement with CAD and the latest move seems like a formalisation of an existing relationship?</b>
<br />
<b>Mohammad: </b>We have been working with CAD for the last two years. We started working with the company on a project for the Oman Tourism Authority two years ago. What became evident from the experience was to come ahead and to expand our offerings in the local market. We saw opportunities in the market and our experience of working together showed how we could deliver bigger benefits to clients by working together.
<br />
<br />

<b>You have jointly designed structures for the Ministry of Tourism. How are these different from other structures on offer?</b>

<br />
<b>Silverstein:</b> Every country exhibit’s or showcases its tourism potential at four key exhibitions – ITB in Berlin, World Travel Mart in London, one in Far East and one in Dubai and these structures offer flexibility.
<br />
<b>Mohammad:</b> The Ministry of Tourism organises 18 different exhibitions throughout the year through which the ministry showcases the country. We offer them a flexible kind of a structure that enables the ministry to use different kinds of sizes and scales for different exhibitions, while making it easier for them to move them around the world.

TBWA\Zeenah already has an events division within its fold, what enhanced value does CAD bring to the table? 
<br />
<b>Mohammad:</b> For the market what CAD is offering is much more specialised, focussed and premium than what we were doing previously. We are looking at scaling up the quality of the work that is on offer in the market and the opportunity that we have with CAD is to offer better services to local clients than what is being currently being delivered. Our event services are very different from what we are offering in association with CAD. Events are more about managing an event and conference but here we are offering branded solutions in terms of the environment and exhibitions for our clients in the market.

<br />
<b>Silverstein:</b> When it comes to events local knowledge and relationships are paramount and that is what TBWA\Zeenah offers in the event area – it takes control of a local event and delivers. It’s not just about coming up with the look and feel, but taking charge and managing the complete logistics of an event. What we look after adds to that skill and complements it with an international design service and that expertise can be taken to a wider market. 
<br />
<br />
<b>What is the potential of Oman’s market from a business point of view? 
</b><br />
<b>Silverstein:</b> This market has huge potential, from CAD’s point of view we could not have been in a better position to enter the market, than with a credible partner like TBWA\Zeenah. We have been looking at Oman for the last five years and it has tremendous potential. We can help Omani businesses in Oman in their global reach and positioning. Oman Tourism Authority and Oman Air have to become international brands and they have to be seen by international market players and that is the opportunity that we see with this partnership. 
<br />
<b>Mohammad: </b>What we are aspiring to is very relevant for what the country stands for -- the quality and durability of a brand and this is something that is very relevant for Oman. It is very critical as to how companies voice out their brands to their clients and how they compete with international brands. It is important that Omani brands have their own distinct identity and space when they go outside the country. 
<br />
<b>Silverstein:</b> We have been here for five years so we come here with a fair bit of understanding of Omani culture and landscape. A clear example of how we can work externally is when the Oman Tourism Authority launched a stand in London, CAD was there to represent TBWA\Zeenah in London. And’s that how the strength of the relationship works. Through this partnership we have a global reach – one through the TBWA\Zeenah network and second through our international reach, offices and clients.
<br />
<br />
<b>TBWA\Zeenah has successfully completed a decade of successful operations in Oman. What are your thoughts on these ten years?</b>
<br />
<b>Mohammad: </b>We have been fortunate to have like minded people in this business and people who have made this success possible. From day one we have been clear about the kind of solutions that we want to offer in the market and our collaboration with CAD is a very good example of that. Our knowledge and expertise is second to none.
<br />
<br />
<b>Zeenah has partnered with specialised domain partners since the very beginning, what was the thought behind this initiative?</b>
<br />
<b>Mohammad:</b> We respect each area of business and when we talk about marketing and communications it is a very wide area and it is impossible for any business to cover all the areas under the marketing umbrella. The only solution to that, is to partner with like minded businesses, who are focussed and specialised on a vertical, and to buy into the knowledge and speciality of those who deliver a better solution. For example CAD with 20 years of experience has that knowhow and knowledge of delivering on the specialised services that we are offering here in the market. We have standalone disciplines in terms of PR, events, media, advertising and branding. We also have specialised capabilities and teams on board to deliver on these areas. 
<br />
<br />
<b>How has the decade long journey with Zeenah been for you personally?</b>
<br />
<b>Mohammad:</b> I am a team person and to start any business one needs to have the right people and from day one we have been clear about the kind of people whom we want on board. The first two people who started the business, are still here and they have been a part of the success of the business. I am just a catalyst and the work is delivered by our respective teams and partners. All of them are delivering on the vision that we have for our business. 
<br />

