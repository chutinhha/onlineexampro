						<p align="justify" class="style10" style="line-height: 14px">
Qatar’s Al Khalij Commercial Bank has inked a deal to finance Alfardan Properties’ up-and-coming Kempinski Marsa Malaz Hotel Resort, with a total investment cost of almost QR1bn. Alfardan Properties, one of the companies under Alfardan Group umbrella, is renowned for luxury turnkey residential and commercial developments in addition to five-star hospitality projects such as Kempinski Residences and Suites. “As the country looks to the future, and continues with its economic growth, companies such as Al Fardan Group Holding will be at the forefront of Qatar’s development,” said Robin McCall, Al Khaliji’s group CEO. “We are therefore privileged to be associated with one of the most ambitious hospitality projects in Qatar – the Kempinski Marza Malaz Hotel Resort. In Al khaliji, we have set about growing a next generation bank as we aspire to become one of the most highly rated and well-respected banks in Qatar. This milestone agreement is a confirmation that Al Khaliji is a reliable business partner and a strong source of financing for development projects in all sectors in the country.”
