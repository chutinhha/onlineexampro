				<br />
				<br />
 				Swiss International Air Lines (SWISS) serves 69 destinations in 37 countries all over the world from its Zurich hub and the further Swiss international airports of Basel and Geneva with a fleet of 91 aircraft. As the airline of Switzerland, SWISS is a byword for traditional Swiss values. With its manageable medium size, SWISS is optimally equipped to remain as close as possible to its customers and meet their individual needs.
				<br />
				<br />
 As the airline of Switzerland, SWISS puts a clear and keen emphasis on the quality of the food and beverages that it serves aboard its flights. To this end, SWISS works closely and continuously with Swiss-based producers and top chefs from all over the country to present a range of inflight food that showcases the best of Switzerland’s cuisine. With its award-winning “SWISS Taste of Switzerland” inflight culinary programme and the “SWISS Traditions” concept that was launched in February 2012 for its European services, SWISS offers regional specialities from all over the country aboard its flights. At the same time, SWISS also collaborates with renowned hotels around the world to offer dishes of equally outstanding quality to its guests from other cultures. The result of all these endeavours is a range of inflight fare that deftly reflects both SWISS’s strong Swiss roots and the global scope of its air travel services.
				<br />
				<br />
 Says Sarah Klatt-Walsh, head of In-flight at SWISS, who has been involved in the development and implementation of the SWISS Taste of Switzerland Gastronomic concept from its inception, “We attach great importance to authentic, balanced, and healthy meals. Our First and Business Class passengers on long-haul flights from Switzerland can enjoy dishes created as part of the SWISS Taste of Switzerland concept. We launched this concept in 2002 and have expanded it continuously since then. In 2004, the gastronomic concept won the renowned Mercury Award conferred by the International Travel Catering Association.”
				<br />
				<br />
 The programme acquaints passengers with the different regions of Switzerland and their culinary specialities. A new Swiss canton is featured every three months. “Since this concept was introduced, we’ve worked with top chefs from Switzerland, who present the particularities and specialities of their home cantons on board,” adds Klatt-Walsh. “They select recipes and menus from their repertoire and act as representatives for regional specialities such as cheeses and wines. The concept is also based on very close collaboration with Switzerland’s regional tourist boards. They support us in our search for local products and suppliers, and also make significant contributions in terms of marketing and communication by providing our passengers with interesting details about the featured cantons.”
				<br />
				<br />
The first step in introducing SWISS Taste of Switzerland on flights back to Switzerland was a partnership with The Peninsula Hong Kong. The hotel has an outstanding international reputation and its kitchen is under Swiss management. In September 2012 SWISS also started introducing a typical Swiss main course on all long-haul flights to Switzerland for Business Class customers.&#8194; 
