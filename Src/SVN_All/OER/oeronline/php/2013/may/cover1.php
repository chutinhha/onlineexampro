<b>DEFINITIONS AND EXPLANATIONS<br />
</b><br />
<b>Revenues:</b> All companies on the list are derived from the published accounts submitted to the Muscat Securities Market (MSM).  Therefore, closed joint stock companies and private companies and establishments are excluded from this list. These companies are, in the first instance, ranked by revenues.  All the other rankings shown on the table do not consider any other companies that do not make the list on the basis of revenue. In the case of banks and investment sector, the gross interest income as well as other operating income together is considered as their revenue for this purpose.  In the case of insurance companies, the gross premium written as well as investment income together is considered as their revenue for this purpose.  All figures are for the year ended on December 31, 2012 or the end of financial year of the company in year 2012, unless otherwise stated. 
<br />
<br />
<b>Profits:</b> Profits are shown after taxes and all charges including extra-ordinary charges. Figures in brackets indicate a loss. All losses and negative growth are also ranked where possible.
<br />
<br />
<b>Assets:</b> Assets shown are as per the balance sheet at the end of the year. It is the total of fixed as well as the current assets.
<br />
<br />
<b>Shareholders’ equity:</b> Shareholders Equity is the paid up capital of the company, retained earnings, and statutory and all other reserves as well as share premium. 
<br />
<br />

<b>Market cap: </b>Market Capitalisation figure has been arrived at by multiplying the total number of outstanding shares of the company by the price per share as of close of business on 31st March 2013 or as the last trading of stock of the company.
<br />
<br />
<b>Earnings per share:</b> The earnings per share are as declared by the company in its published financial statements.<br />
<br />
<b>Dividend yield:</b> The dividend yield figure is calculated on the basis of dividend declared in the financial statements for 2012 against the share price at close of business on 31 December 2012 or as per the end of financial year of the company.
<br />
<br />
<b>Price earnings ratio:</b> This ratio has been calculated by dividing the price per share by the earnings per share as at 31 Dec 2012.
