					<p align="justify" class="style13">
 
This year, the Chrysler Cup is scheduled for November 15 and 16 and will be hosted at the prestigious and stunning Almouj Golf Course. The semi-finals and finals will run over one weekend, and comprise three rounds of golf played over two days. The semi-finals will be held on day one where teams compete first in a fourball round followed by Greensoms. Day two will witness the finals where the victors of day one will compete for the trophy and the other two teams will compete to place in a 'Singles match play' round. 
<br />
<br />
The Chrysler Cup, a Golf initiative introduced for the first time in Oman in 2012, was a great success. The goal behind this initiative was to associate Chrysler with the fastest growing sport in the world, golf, as well as to elevate the level of golf and recognise golfing talent in the Sultanate. 
<br />
<br />
This tournament, run on a format similar to the Ryder Cup, comprises four competing teams of 12 people, each team representing one of the four grass course clubs in Oman: Ghala Valley Golf Club, Almouj Golf Club, Muscat Hills Golf & Country Club, and Ras Al Hamra Golf Club. The players in each team are derived from the Oman Golf Committee's Order of Merit rankings. As a result of the resounding success for the 2012 championship, the event is now considered to be one of the most prestigious golfing events in the country. Commenting on the popularity of the Chrysler Cup, Chris Edwards, general manager of Dhofar Automotive noted, "This tournament engages the entire competitive golfing community in the Sultanate. Competitors play various monthly medals to improve their standing in the Order of Merit, the Oman Golf Committee's ranking platform, to enable them to qualify to represent their club in a grand tournament towards the end of the year. Last year's tournament was an unquestionable success, and participants have been eagerly looking forward to this year's competition."  
<br />
<br />
Without a doubt, if you are a golf aficionado, this is 'The' local golf tournament to watch for and we just can't wait to see who will take home the coveted cup this year. <br />


