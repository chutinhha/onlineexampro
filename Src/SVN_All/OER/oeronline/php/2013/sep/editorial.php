				<b><font face="Verdana" size="4" color="#C00000">Shifting gears</font></b><p align="justify" class="style13" style="line-height: 17px">
				
				<br><p class="style15" style="line-height:14px;">
				<font face="Verdana" size="1">
				oman's healthcare sector has come a long way since the blessed Renaissance. In 1970, there was only two hospitals and a handful of admission units in Muscat. Cut to 2013 and the picture is remarkably different. The Sultanate today boasts of 65 hospitals with 5,859 beds and 1,143 health centres.
 
				<br />
				<br />Life expectancy has kept pace with better facilities. Average life expectancy at birth stands at over 72 years, compared to 49 years in 1970. Communicable diseases such as measles, malaria and typhoid have been eradicated and infant mortality rate has also dropped from 29 to 9.5 deaths for every 1,000 live births.  

				<br />
				<br />
While the progress made in the healthcare has been nothing short of remarkable, in recent years there has been another significant shift in the sector with hospitals and polyclinics moving up the value chain. With primary healthcare being well spread, new players are looking at secondary and tertiary care as the next frontier. So you have a new sugar clinic offering specialised treatment for diabetes and other related ailments. Others are focusing on orthopaedics, heart related ailments and plastic surgery. MuhammedNafie, OER's Senior Sub-Editor does a deep dive into the sector to bring readers up to speed with the frenetic pace of development in the healthcare sector in this issue's cover story.
