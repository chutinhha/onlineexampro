						</b></font><b><font face="Verdana" size="2">Sultanate's LNG industry enters a new era as Oman LNG and Qalhat LNG merge to form one company</font></b>
						
						<br /><br />
						<center>
						<img src="images/Inthenews/2.jpg" border="0"/> <br><div style="width:260px; font-size:12px" >HE Dr Mohammed bin Hamad Al Rumhy, Minister of Oil and Gas and Chairman of Oman LNG</div></center>
						
						<p align="justify" style="margin: 3px 10px"> 
					<p class="style15">
<font face="Verdana" size="1">
The Sultanate's liquefied natural gas industry entered a new era on September 1, 2013 assuming a bolder, more unified front with the fusion of the country's only two liquefied natural gas companies – Oman LNG and Qalhat LNG, into one fully integrated entity. The merger has been approved by the shareholders of both formerly distinct corporations.
<br />
<br />
The integration capitalises on a number of existing operational efficiencies and potent synergies such as production and export from common facilities at the three-train plant in Sur, one source of gas supply in Petroleum Development Oman (PDO), same shipping provider in Oman Shipping Company (OSC) and almost similar destinations of liquefied natural gas cargoes. It will also take advantage of the combined knowledge of highly skilled workforce in the country's LNG industry and offers greater flexibility to deploy a larger portfolio of cargoes and vessels in the global liquefied natural gas market.
<br />
<br />
 The merger has the potential to enhance revenues accruing to the nation from LNG exports by reducing cost of operations and offering better management of swaps and diversions of cargoes while maintaining a reliable supply of LNG cargoes to long-term buyers. Enhanced customer focus and care is also a paramount feature of the integrated entity.


<br />
<br />
The integration brings together the strengths of the two companies, positioning the new entity to leverage on supply opportunities expected to come with projected growth demand in the global LNG industry. While Oman LNG's business has emphasised on the sale of its cargoes through long-term sales and purchase agreements (SPAs), Qalhat LNG is an active and successful player in swaps and diversions of LNG cargoes as well as long-term SPAs.
<br />
<br />
"The successful integration of Oman LNG and Qalhat LNG, marks the commencement of a new era for our nation's LNG industry," says HE Dr Mohammed bin Hamad Al Rumhy, Minister of Oil and Gas and chairman of Oman LNG. "Both companies have a solid track record of achievements in terms of reliable supply of LNG to our valued customers, high revenue generation for the country, contribution to social development and Omanisation of staff. As one solid entity, all stakeholders in the value chain can be assured that even greater value will be generated from the operations in the industry. Oman now has one face for LNG exports to the world and this news has been well received by the global markets," HE Rumhy adds.
<br />
<br />
<b>Vision statement</b>
<br />
<br />
Oman's liquefied natural gas industry has grown out of the vision of His Majesty Sultan Qaboos bin Said to diversify the country's economy and has attracted high revenue by harnessing natural gas resources for export as liquefied natural gas. The country produced its first shipment of liquefied natural gas in 2000 after the first of an initial two-train plant began operations under Oman LNG, established by His Majesty's Royal Decree in 1994. With a third train under Qalhat LNG, liquefied natural gas has played an even greater role in contributing to the national economy as the two companies have worked intensely with outstanding success to drive their organisations forward. 
<br />
<br />
"Both Qalhat and Oman LNG stakeholders will derive much more value from the unified, holistic front for liquefied natural gas exports in the new integrated company," said HE Nasser bin Khamis Al Jashmi undersecretary at the Ministry of Oil and Gas and Chairman of Qalhat LNG.
<br />
<br />
"The integration of Oman LNG and Qalhat LNG is a milestone that heralds the preparedness of the country's LNG industry to remain a key source of stable and reliable supply to all our customers in the gas market, a key source of revenue generation for Oman and its people and valuable contributor of sustainable growth to the many communities all across Oman that we serve through our many social investment programmes," said Harib Al Kitani, CEO of Oman LNG, the operating name of the new entity. The integrated company, Oman LNG, will operate from its two offices in Muscat: the Qalhat LNG property situated at Airports Heights, Ghala and the familiar Oman LNG building located at Shatti Al Qurum.
