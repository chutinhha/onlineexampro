						</b></font><b><font face="Verdana" size="2">Within a short span of nearly three years, Better Homes has established itself as a leading player in Oman's property market. The company is looking to emerge as a one-stop shop offering a wide array of services, says it's General Manager, Glenn Meek

</font></b><p align="center" style="margin: 3px 10px"> 
					<p align="center" style="margin: 3px 10px"> <br />
