With the National Bank of Oman Golf Classic getting ever closer, the interest created by the Sultanate's first professional golf tournament is fast gathering momentum. 
<br />
<br />
The leading players on the European Challenge Tour are looking forward to visiting Oman for the first time and taking up the challenge at Almouj Golf, The Wave, Muscat, Oman from October 24 to 27 in one of the strongest fields of the tour's 2013 schedule.
<br />
<br />
Oman's leading amateur, Azaan Al Rumhy, now a scratch golfer, can't wait for the action to start. "I'm excited to be competing against the future stars of The European Tour and I believe the Arab nationals who have proved their ability in international tournaments this season, supported by the National Bank of Oman, will put in a good performance. I'm also pleased that my long-time Oman National team-mate, Ali Hameed, received a sponsor's invitation – he's delighted to be taking part," says Azaan, who is also looking forward to the Chrysler Cup in November where he will lead his defending champions, Ghala Valley, against Amouj Golf, The Wave, Muscat, Muscat Hills and Ras Al Hamra, Muscat.  
<br />
<br />
<strong>Gaining in popularity</strong>
<br />
<br />
The National Bank of Oman Golf Classic is certainly capturing the imagination of the golfing and business community throughout the Middle East and internationally. The support the tournament is gaining week-on-week is phenomenal.
<br />
<br />
"It's exciting to witness the increasing interest in the National Bank of Oman Golf Classic in the Sultanate and further afield," says Mohammed Mahfoudh Al Ardhi, deputy chairman, National Bank of Oman, whose title sponsorship and enthusiasm have inspired such widespread acclaim for the tournament.
<br />
<br />
"It was our mission in sponsoring the National Bank of Oman Golf Classic that we would not only put Oman on the world golf map but that we would bring the business world's attention to the considerable benefits of Oman to international companies," says Mohammed.
<br />
<br />
"The amount of promotion and publicity worldwide that has already been generated since we announced the event earlier in the year has been very gratifying and will no doubt have a bearing on golf tourism and business in Oman."
