						<font face="Verdana" size="4" color="#C00000">Protecting an endangered species</font></b></p>
						<p align="justify" style="margin: 3px 10px; line-height:17px"><b><font face="Verdana" size="2">
                		<br>The National Centre for Field Research in Environment Protection will host a conference highlighting the importance of protecting Arabian leopard, in September 2013<br /></font></b>

<br />
<center><img src="images/Environment/1.jpg"></center>
<br/>
						<p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
The Arabian leopard, the last remaining big cat in the Middle East, is almost invisible in the canyons and dry riverbeds of Oman. Once thriving across the Middle East, this species is now considered critically endangered by the International Union for Conservation of Nature. It estimates there are fewer than 200 in the wild with the largest single population of 50 to 100 in the mountains of southern Oman. 
<br />
<br />

Historically, Arabian leopards lived in the mountain areas in the Sultanate, Jordan, Palestine, Saudi Arabia, Emirates and Yemen. But by the end of the 20th century, it became extinct from almost all these areas. Yet leopards are still colonising high mountains in the Sultanate, mostly in JabalSamhan in Dhofar in a special reservation built by the governorate. The Arabian leopard lives in Dhofarmountains, which extends from JabalSamhan in the east to Jabal Al Qamar in the west, near to Oman-Yemen borders. According to a recent survey, currently there are 25 leopards in JabalSamhan, while there were only 17 leopards, according to the study conducted between 1997 and 2000.
<br />
<br />
The Sultanate's National Centre for Field Research in Environment Protection (NCFR) will host a conference highlighting the importance of protecting Arabian leopard, in September 2013. The conference, aimed at setting up a national plan to preserve this endangered species, will be attended by the representatives from the Ministry of Environment and Climate Affairs, Environment Reserve Office at the Royal Court, and State Minister and Dhofar Governorate Office. The conference is expected to result in more coordinated approach to saving the leopards.
<br />
<br />
The first phase of a government project to protect Arabian leopard has succeeded in tracing their places of existence and identifying their frequent behaviours and attitudes via special hidden cameras located professionally up high in the mountains. The cameras, which videoed 17 leopards in the first three years of the survey, were widened later to include most of the western mountains of Dhofar. By the year 2008, 12 leopards were figured in Salalahmountain and Jabal Al Qamar. Experts still watch the movements of leopards thorough the hidden cameras to obtain important information about them which could help in their protection. Some videos were also recorded to produce a special film about Arabian leopard in the Sultanate, which was recognised to be the first of its kind in the Middle East.
