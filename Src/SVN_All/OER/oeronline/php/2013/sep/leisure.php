<b>Can you throw more light on 'Light of Life' and its various initiatives?</b>
<br /><br />
Light of Life was created to clear the clouds of darkness in people's lives, to give people a chance to experience lives full of joy and fulfillment. It is especially created for the less-privileged and orphans, for their development in life. The objectives of Light of Life broadly classified under three verticals are:<br /><br />
<b>Anando:</b> Education. To realise the untapped potential of rural children<br />
<b>Awakening:</b> (Jagruti) Holistic Community living<br /> 
<b>Angan:</b> Healthcare, with alternative therapies to allopathic medicine and a hospice for the terminally ill. 
<br />
<br />
The first initiative of Light of Life was to focus on promoting education and health in far-flung villages of Gujarat and Maharashtra in India. Light of Life has so far impacted and transformed the lives of more than 9,000 children through sustained education, vocational training and created employability.
