Oman Police (ROP Emergency &amp; ROP Traffic Safety), Ministry of Health and Oman 
Women Association (Seeb), held summer and road safety campaigns under the theme 
“Survive this Summer With Water, Rest &amp; Care” and “Drive For your Family”. The 
campaigns held from June 8 to 15 were designed to raise awareness and involve 
citizens and residents to prepare themselves to protect against heat and road 
hazards. As part of the campaigns, special events took place at Al Qurum 
National Park and Muscat City Centre. “The event at Al Qurum Park was meant to 
spread awareness about heat and fatigue, its symptoms and protections measures,” 
said Neelesh Sogani, corporate HSE manager, Powertech. The Ministry of Health 
carried out health monitoring for the spectators and advised them on healthy 
practices. The event also included blood donation camp. ROP (Emergency) 
generated awareness on emergency response especially during domestic fire, heat 
