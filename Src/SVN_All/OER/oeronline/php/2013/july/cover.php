						Green Salute</font></b><p class="style15">
				<span class="style14"><strong><font size="2">Marking the World Environment Day on June 6, outstanding environmental efforts by individuals, government organisations and corporates were felicitated at the fourth edition of Oman Green Awards (OGA) at Hotel Intercontinental Oman</font></strong></span><p class="style15" style="line-height: 14px">
						<p class="style15" style="line-height: 14px">
						<span class="style14"><font style="font-size: 8pt">
