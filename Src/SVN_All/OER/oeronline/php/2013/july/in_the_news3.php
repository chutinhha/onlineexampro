<font face="Verdana" size="2"><b>Best Banks and Best NBFCs awards presented</b></font><font size="2"><br />
&nbsp;</font><center>

<img border="0" src="images/in_the_news_3.jpg" width="442" height="166">
</center>
<p class="style15" style="line-height: 14px">

<br />
The Best Banks and the Best NBFCs in Oman 2012 awards instituted by OER and Gulf 
Bader Capital Market (GBCM) were conferred on Ahlibank and Al Omaniya Financial 
Services respectively. Abdul Aziz al Balushi, CEO of Ahlibank and Aftab Patel, 
CEO of Al Omaniya Financial Services, received the trophies from Sandeep Sehgal, 
chief executive of United Media Services (UMS), Alpana Roy, executive 
vice-president of UMS and Mayank Singh, group editor of OER.<br>
The OER-GBCM survey of the Best Banks in Oman in 2012 considered the growth and 
sustainability parameters for annual ranking of the commercial banks in Oman. 
The banks’ ranking model was done among the leading local banks which include 
Bank Muscat, National Bank of Oman, Oman Arab Bank, Bank Dhofar, HSBC Bank Oman 
(erstwhile Oman International Bank), Ahli Bank and Bank Sohar. As part of its 
new initiative to bring on board the leasing sector performance, OER-GBCM has 
commenced its annual ranking of the best leasing companies in Oman starting this 
year. For this annual ranking process, the survey has considered the growth and 
sustainability parameters of the leasing companies in Oman, similar to the best 
banks ranking model with few changes relevant to the sector. The leasing 
companies ranking model was done among the local players which include Al 
Omaniya Financial Services, National Finance, Muscat Finance, United Finance, 
Taageer Finance and Oman Orix Leasing. The non-banking financial companies (NBFCs), 
licensed by the Central Bank of Oman (CBO), grew by 23 per cent in 2012 in terms 
of asset size.<br />

