<font size="2"><b>Al Shamikh warship handed over to RNO</b></font><p class="style15" style="line-height: 14px">
<center>

<img border="0" src="images/in_the_news_4.jpg" width="351" height="166">
</center><p class="style15" style="line-height: 14px">
The first warship built by BAE Systems for the Royal Navy of Oman (RNO) as part 
of Project Khareef for the design, build and delivery of three corvettes, has 
been formally handed over in a ceremony at HM Naval Base Portsmouth recently. 
Employees and guests, including senior representatives from the Royal Navy of 
Oman and UK Royal Navy, gathered in front of the ship as the crew marched on 
board to raise the national flag of Oman on her flight deck for the first time.<br>
<br>
“Today is an important milestone in the strong and growing relationship between 
BAE Systems and Oman, as we hand over the first of the Khareef class warships,” 
said Mick Ord, managing director of BAE Systems Maritime – Naval Ship.<br>
<br>
“It’s an immensely proud moment for everyone at BAE Systems involved in the 
design, build, trial and delivery of Al Shamikh to see her crew march onboard 
and take ownership of the vessel. We now look forward to continuing our 
partnership with the Royal Navy of Oman, supporting the new crew and working 
towards the completion of the next two ships.”<br>
<br>
Commodore Rashid AL-Raisi from the Royal Navy of Oman said, “We are certain Al 
Shamikh will be an inspiration to the men of the Royal Navy of Oman. She will be 
our flagship and the pride of the Sultan’s armed forces.”<br>
<br>
The Khareef Class corvettes are equipped to defend against both surface and air 
threats with stealth features to operate undetected, an innovative hull design 
and electric propulsion to provide exceptional manoeuvrability.</td>
