<p align="justify" class="style13">
The Sultanate of Oman has been a land of amazing natural beauty, rich history and culture and above all, a friendly society with people of great talent. His Majesty Sultan Qaboos Bin Said’s dynamic leadership for 43 years has created a perfect environment for people from all walks of life to pursue their interests and desired goals. As a result, Oman has a new generation of young achievers who have risen above the ordinary to become role models in their own right. 
<br />
<br />
<img border="0" src="images/pupl_1.jpg" width="145" height="210" align="left" />

 JAWHARAT OMAN – SUPER ACHIEVERS OF THE SULTANATE, the first bilingual (Arabic & English) publication of its kind by United Media Services, is a chronicle of such incredible Omanis with extra ordinary achievements. 
 
<br />
<br />
 The third edition of Jawharat Oman showcases 25 super achievers. Once again, they are a set of diverse individuals with proven achievements in their fields of specialisation but they have certain common binding traits – the thirst to go beyond the ordinary and achieve their goals, despite challenges in their paths. They are individuals who have risen above the ordinary to become champions in their chosen fields.
 
<br />
<br />
 According to Alpana Roy, executive vice president, UMS, “Once again, Jawharat Oman will bring its readers up close and personal with those who are shaping Oman’s cultural, social and scientific landscape. I firmly believe that Jawharat Oman will be a prized possession for all and serve as a window to showcase the Sultanate’s astounding talent to rest of the world.”
 
<br />
<br />
 Take a different kind of journey and explore the stories of photographers, artists, sportspersons and more – all of whom have gone above and beyond expectations to rise above the crowd. Learn about their setbacks, their inspirations, discover the unique qualities that they possess and above all, be inspired to create your own success story.
 
