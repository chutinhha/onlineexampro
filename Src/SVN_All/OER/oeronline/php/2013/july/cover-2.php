<b> A veritable green school
</b><br />
<br />
 Ras Al Hamra International School which is currently being built by PDO has several inbuilt green building features enabling it to conform to the school rating system requirements of Leadership in Energy and Environmental Design (LEED), an international agency
<br />
<br /> 
Ras Al Hamra Development Project has incorporated several state-of-the-art green features into its new international school which will be the first school in Oman certified by LEED (leadership in Energy and Environmental Design), an internationally recognised rating system for the design, construction and operation of high performance green buildings, homes and neighbourhoods.
A number of main LEED methods and principles were applied in the design and construction of the school such as intelligent and sustainable design, incorporation of energy efficient methods, minimising water consumption and using treated water, less natural resources and rapidly renewable and recycled materials etc. The learning environment of the school will have high daylight ratio, a higher supply of fresh air and more efficient toilet and recycled water supply systems. Everything about the fabric of the building is about energy efficiency. Environmentally conscious thinking is at the forefront of the redevelopment with the alignment of new buildings taking into account the sun’s trajectory to enable solar-powered streetlight and hot water systems. The new school will also incorporate all sustainable features into current IPC curriculum which also addresses sustainable/ environmental awareness lessons and tools. Sustainable curriculum will be further expanded into detailed curriculum lessons covering all age groups/ grades.

