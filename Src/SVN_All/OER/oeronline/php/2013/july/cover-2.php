<b> Turning waste into fertile soil
</b><br />
<br />
 The remediation of drilling wastes by Free-RAD technology implemented by Ray Eco-Logic can convert contaminated waste into fertile soil, by reducing the waste up to below 1 per cent
<br />
<br />
Ray Eco-Logic has introduced an initiative for remediating drilling wastes by using Free-RAD technology, which can reduce the concentration of total petroleum hydrocarbons (TPH) contaminated waste up to below 1 per cent and to convert the contaminated waste into a fertile soil. The process turns contaminated material into fertile soil good for raising crops, and can be replicated in many places where the drilling waste has been accumulating for years causing damage to the environment. The company has measured the success of the programme by sampling and analysing the TPH content along the process and by comparing it to the time needed to achieve the results.
Implemented for the first time in the GCC, the project has proved more effective compared to other land farming techniques, and raised the interest of other operators in the GCC and other countries to apply this technique on their sites. The project could also convince the user and the authorities that this technique could be applied to reduce the great environmental liability that these wastes represent.&#8194;


