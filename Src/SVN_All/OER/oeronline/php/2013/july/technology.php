				<b><i><font face="Verdana" size="2">
				The Internet of Everything (IoE) economy is creating new opportunities to transform the world around connected devices, people and processes in education, healthcare, manufacturing, commerce, transportation and other sectors. But with these new opportunities come challenges and check points with respect to data security and privacy that CSOs will need to cover. This article gives an insight into the security considerations that organisations and businesses of all sizes in Oman will be faced with as the IoE takes effect</font></i></b>
