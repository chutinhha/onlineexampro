						<span class="style27"><strong>Oman Forum calls for integrated efforts for job creation</strong></span><p align="left" class="style13" style="line-height: 17px">

						<br />
						<br />
						<center><img src="images/events_1.jpg"></center>
						<font size="1" face="Verdana">
						<br />
						<br /><p align="justify" style="margin: 3px 10px" dir="ltr">
 Human resource development and improving the quality of higher education in the country to meet the new requirements of job market have been and will continue to be the priority area of government policies, stated HE Darwish bin Isma’eel bin Ali al Balushi, Minister Responsible for Financial Affairs. Replying to the questions raised during the panel discussions at Oman Forum: Job Creation – The National Agenda, organised on May 6, 2013, at Oman Auditorium, Al Bustan Palace Ritz-Carlton, he said the government will look into the ways to increase the productivity of the national workforce on a par with the international standard. Highlighting the importance of in-job training, HE Darwish called on the private sector to invest more on training and equipping their employees. “The private sector cannot expect the government to give them fully equipped employees to do the job,” he added.
						<br />
						<br />
The annual Arabic event was organised by Alam al-Iktisaad Wal A’mal (AIWA), Oman’s premier business magazine published by United Press & Publishing (part of UMS Group). The half-day long event comprised two panel discussions on the solutions for maintaining a balance between job creation and business growth as well as promotion of small and medium enterprises. The relevant stakeholders from all walks of the society including the Government, private sector, HR experts and civil society deliberated on the various dimensions of job creation.
The event was supported by the Ministry of Manpower, Ministry of Higher Education, Ministry of Civil Services, Sultan Qaboos University, OPAL, Oman Society of Contractors and The Research Council (TRC). Oman Oil Company, Ahli Bank and Galfar were the Strategic Partners whereas Petroleum Development Oman (PDO) and Oman Cables Industry were the Associate Partners; and Omantel, Nawras, Jindal Shadeed Iron & Steel and Al Jazeera Steel Products Company were the Support Partners for the prestigious forum. Times of Oman and Al Shabiba were the Media Partners for the event. Noted TV anchor Yusuf al Hooti was the moderator of the forum.
						
						<br />
						<br />
Setting the tone for no-holds-barred discussions and heated exchanges on the challenges of job creation and business growth, HE Mohsin al Balushi, advisor, Ministry of Commerce and Industry opined that job creation is a collective responsibility of the government, the public and private sector companies. “The role of the government is to create an atmosphere conducive for job creation through investment in education and infrastructure development,” he said. 
Replying to a question during the panel discussion, HE Dr Muna Al Jardaniya, undersecretary at the Ministry of Manpower, said, “The Ministry of Manpower accords more importance to training employees in general. The focus of training is both to equip the employees with the capabilities required for their existing jobs and to empower them with the skillsets and professional expertise essential for the upcoming opportunities in the job market.”
						<br />
						<br />
						<center><img src="images/events_2.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px" dir="ltr">
Salim bin Nasir Al Hadhrami, director general of training and planning at the Ministry of Manpower, opined the recent decision to increase the minimum salary will be helpful in drawing more people to the private sector and reduce the migration of employees to the public sector, though it may be initially challenging for small and medium sector companies.						
<br />
<br />
Among the other panelists, Hussain Salman Al Lawati, MD, Oman Cables Industry, observed that in order to increase the productivity of employees there is an urgent need to inculcate values of work culture and love for job among the youth. “Private sector companies who employ a large number of Omanis require more people who are productive and efficient. Work culture is not something which one learns from the school, university or through training. It has to start from home. People have to be taught to love their work instead of loving their salary.”
<br />
<br />
Ahmed Al Wahaibi, CEO, Oman Oil Co. said: “Everybody has a role to play in creating more job opportunities for the Omanis. Companies should concentrate more on training the employees to meet the changing demands. They have to give training to their employees in Oman instead of sending them outside the country. ”
<br />
<br />
It must be noted that a set of recommendations based on the panel discussions and public opinion at Oman Forum has been submitted to the Minister Responsible for Financial Affairs and the Minister of Commerce & Industry.&#8194;
