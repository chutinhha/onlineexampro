						Rationality conundrum</font></b><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="2">
				<b>Despite the heavy intellectual strain, Thinking, Fast and Slow is recommended reading for the academically inclined and the laity.<br />
				<i> An OER report</i></b></font><p align="center" style="margin: 10px; ">
				<p align="justify" style="margin: 10px; ">
				
				<font face="Verdana" size="1"><img border="0" src="images/browsing_corner_1.jpg" align="left" hspace="2" vspace="2">
 In 2002, Daniel Kahneman won the Nobel in economic science. What made this unusual is that Kahneman is a psychologist. Specifically, he is one-half of a pair of psychologists who, beginning in the early 1970s, set out to dismantle an entity long dear to economic theorists: that arch-rational decision maker known as Homo economicus. The other half of the dismantling duo, Amos Tversky, died in 1996 at the age of 59. Had Tversky lived, he would certainly have shared the Nobel with Kahneman, his longtime collaborator and dear friend.
				<br />
				<br />
 Human irrationality is Kahneman’s great theme. There are essentially three phases to his career. In the first, he and Tversky did a series of ingenious experiments that revealed twenty or so “cognitive biases” — unconscious errors of reasoning that distort our judgment of the world. Typical of these is the “anchoring effect”: our tendency to be influenced by irrelevant numbers that we happen to be exposed to. In the second phase, Kahneman and Tversky showed that people making decisions under uncertain conditions do not behave in the way that economic models have traditionally assumed; they do not “maximise utility.” The two then developed an alternative account of decision making, one more faithful to human psychology, which they called “prospect theory.” (It was for this achievement that Kahneman was awarded the Nobel.) In the third phase of his career, mainly after the death of Tversky, Kahneman has delved into “hedonic psychology”: the science of happiness, its nature and its causes. His findings in this area have proved disquieting — and not just because one of the key experiments involved a deliberately prolonged colonoscopy.
				<br />
				<br />
<b> Three phases
</b>				<br />
				<br />
 Thinking, Fast and Slow spans all three of these phases. It is an astonishingly rich book: lucid, profound, full of intellectual surprises and self-help value. It is consistently entertaining and frequently touching, especially when Kahneman is recounting his collaboration with Tversky. (“The pleasure we found in working together made us exceptionally patient; it is much easier to strive for perfection when you are never bored.”)
				<br />
				<br />
A leitmotif of this book is overconfidence. All of us, and especially experts, are prone to an exaggerated sense of how well we understand the world — so Kahneman reminds us. Surely, he himself is alert to the perils of overconfidence. Despite all the cognitive biases, fallacies and illusions that he and Tversky (along with other researchers) purport to have discovered in the last few decades, he fights shy of the bold claim that humans are fundamentally irrational.			
				<br />
				<br />
 “Most of us are healthy most of the time, and most of our judgments and actions are appropriate most of the time,” Kahneman writes in his introduction. Yet, just a few pages later, he observes that the work he did with Tversky “challenged” the idea, orthodox among social scientists in the 1970s, that “people are generally rational.” The two psychologists discovered “systematic errors in the thinking of normal people”: errors arising not from the corrupting effects of emotion, but built into our evolved cognitive machinery. Although Kahneman draws only modest policy implications (e.g., contracts should be stated in clearer language), others — perhaps overconfidently? — go much further. Brooks, for example, has argued that Kahneman and Tversky’s work illustrates “the limits of social policy”; in particular, the folly of government action to fight joblessness and turn the economy around.&#8194;



