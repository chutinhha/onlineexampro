						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Turn on the heat 
</font></b></p>
						<p align="justify" style="margin: 3px 10px; line-height:17px"><b><font face="Verdana" size="2">
                		<br>
Under the “Vision 2020” policy, Oman is dedicated to produce 10 per cent of its total electricity requirement from renewable energy sources like solar and wind power and reduce dependence upon fossil fuels. A report by Visvas Paul D Karra on the next energy frontier for the Sultanate<br /></font></b>

<br />
<br />
<center><img src="images/env_1.jpg" border="0"></center>
<br />
<p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
According to an Ernst & Young report, there are numerous challenges to generate renewable sources of energy: Firstly, the need for huge levels of energy infrastructure investment in relation to generation capacity and grid infrastructure, in a climate when both government and private sector funds (particularly in the form of bank finance) are constrained. Secondly, the desire to diversify towards a more mixed energy economy with a significant component of renewables by 2020, with a view to taking further advantage of these technologies (once grid parity is achieved), thereby lessening the dependence on imported fossil fuels.
<br />
<br />
 In the Sultanate of Oman, the seed for renewable sources of energy was laid way back in 2005, when the country, under the far-sighted leadership of His Majesty Sultan Qaboos bin Said, signed up to the Kyoto Protocol and announced the formation of a designated national authority in 2009 to encourage and administer the utilisation of renewable energy.
<br />
<br />
 However, solar energy systems have been in use for powering telecommunications and monitoring equipment in the Sultanate’s remote desert and mountain areas since the early 1990s. 
<br />
<br />
 The comprehensive report on renewable energy issued by Oman’s Authority for Electricity Regulation (AER) in 2008, known as Renewable Energy Study, identified solar power as one of the Sultanate’s top prospective energy sources for the coming decades. This is line with the government’s focus to help diversify the national economy and to meet growing domestic energy needs and reduce dependence upon ever depleting fossil fuels.
<br />
<br />
 The AER report has noted that Oman’s solar energy density ranks among the highest in the world and it estimated that, theoretically, it would be possible to produce sufficient electricity to satisfy all of Oman’s electricity consumption at present levels by utilising 280 sq kms of desert that is just 0.1 per cent of the Sultanate’s total land area for solar collectors.
<br />
<br />
<b> Renewable cluster
</b>
<br />
<br />
 “The more important thing within Oman is the push from His Majesty as well to actually increase the utilisation of the natural resources like solar and wind. The other aspect which comes along with that is this whole value chain,” says Naveen Kandukuri, director, Transaction Advisory Services, Ernst & Young. Kandukuri has been closely associated with the power sector in Oman.
<br />
<br />
According to him, we can have various technologies for harnessing solar and wind power like photo voltaic and concentrated solar power. If you probably look at some of the energy players who have talked about this in Oman in the past, they don’t come about this as one single investment in a renewable power facility. They actually start bringing in the value added facilities which is the manufacturing for the PV panels and other things. They also talk about the education cluster which comes along with that. And all of that is a renewable cluster where you get technology, competitors, the regulator and a clear framework for developing the industry as well as the economy as whole. All of that helps to develop an industry in a particular region. For example, thats the reason why all the textile shops are located in one area in a place like Mumbai.
<br />
<br />
The most important thing that affects the government and the private sector in return is the fact that fossil fuel is a diminishing utility while its price keeps increasing all the time, says Rajeev Singh, partner, Transaction Advisory Services, Ernst & Young.
<br />
<br />
The price of fossil fuels like oil and gas has increased over the past decade. So you look at renewables which is a natural resource. Now the only costs that you incur in renewable is the cost of equipment and the efficiency levels. Soon the tipping points between fossil and renewable is getting narrower which is a great incentive to go for alternate energy sources like solar and wind.
<br />
<br />
<b>Pilot projects underway
</b><br />
<br />
The Renewable Energy Study made a number of recommendations for harnessing the Sultanate’s renewable resources including a recommendation that the Authority support the implementation of renewable energy pilot projects in conjunction with Rural Areas Electricity Company (RAECO) in rural areas. The authority accepted the pilot project recommendation and invited developers to submit pilot project proposals for rural locations supplied by RAECO diesel generation.
<br />
<br />
Following, this RAECO has started working on six renewable energy projects, including four solar and two wind power ventures as part of its five year (2013-2017) plan. The company identified sites for these projects in different governorates, including one in Musandam for solar energy; three in Al Wusta and two in Dhofar (one in Al Mazyuona and the other in Almtafh near Thamrait).
<br />
<br />
Hamed bin Salim al Magdheri, CEO, RAECO, has reportedly said that the company is working on two other projects using windmills in Masirah and Thamrait. These power plants can produce 200 to 300 KW. The company is in an advanced stage to start building a 350kW-capacity solar and a 750kW wind power project in Al Mazyuona and Masirah.
<br />
<br />
Al Magdheri said it is feasible to set up solar power projects, than depending on diesel-based power. The company produces electricity in its 48 diesel-based power plants because gas is not available in its concession areas and its cost of production (79 baisa per kilo-watt) is much higher than that of gas-based power plants, which is less than 15 baisa. The electricity is provided to people in Oman with a 70 per cent subsidy.
<br />
<br />
As a first step, RAECO is building a solar power plant in Al Mazyuona in Dhofar which is expected to be ready in the second quarter of 2014. 
<br />
<br />
The six projects will offer 6.6MW of renewable capacity at an investment cost of around RO8.1mn. The six shortlisted projects, if implemented, would allow RAECO to replace 10,683MWh of annual diesel generation with renewable sourced electricity, and reduce diesel fuel consumption by 3.1 million litres per annum thereby avoiding 8,298 metric tonnes of CO2 per annum.
<br />
<br />
The shortlisted projects offer economic benefits in the form of the avoided costs of diesel generation and other potential benefits such as the transfer of knowledge and experience to Oman, capacity building within RAECO and other Oman entities and companies.
<br />
<br />
<b>Bright and shining!
</b><br />
<br />
Meanwhile, the Public Authority for Electricity and Water (PAEW) has reportedly identified four potential locations for the establishment of large-scale solar power projects with land being allocated at two of these places. Dr Ali bin Hamed al Ghafri, assistant chairman for International Relations and Media at PAEW, says that as many as 23 locations were identified and tested in the study. According to reports quoting Dr Ali, the north of Oman was found most feasible for solar power projects, while wind power projects could be located in southern Oman.
<br />
<br />
Land at Adam and Manah has been identified for two large scale solar power projects ranging in the 100MW-200MW area. But it has yet to be decided whether these plants will produce power using the photovoltaic technology (PV) or concentrated solar power (CSP) technology. The other two locations for solar power are Al Khaboura and Ibri.
<br />
<br />
<b>Sun to enhance oil recovery</b>
<br />
<br />
In the quest for renewable energy, The Petroleum Development Oman may perhaps go down as the first to draw blood thanks to the successful implementation of the Middle East’s first solar enhanced oil recovery (EOR) project at the Amal West field in Southern Oman. The project while freeing up significant amount of gas which would have been used for steam production has also cut down on the environmental impact of burning natural gas.
<br />
<br />
PDO, the largest producer of oil and gas in Oman in association with GlassPoint Solar, the global leader in solar enhanced oil recovery, successfully commissioned a project wherein the solar EOR project will produce a daily average of 50 tonnes of emissions-free steam that feeds directly into existing thermal EOR operations.
<br />
<br />
The seven megawatt system, which is in regular operation, harnesses the sun’s energy with GlassPoint’s Enclosed Trough technology, and can cut natural gas used for EOR by up to 80 percent, helping Oman release its natural gas resources for higher value applications such as power generation, desalination, industrial development or export as LNG.
<br />
<br />
Rod MacGregor, CEO, GlassPoint, says that construction on the project began in January 2012 and finished in December 2012. The project was completed on time, on budget and with no Lost Time Injuries in accordance with GlassPoint and PDO’s shared commitment to sound engineering execution and health, safety, security and environment leadership.
<br />
<br />
Today, EOR applications account for a significant portion of Oman’s annual natural gas consumption. By incorporating solar steam, PDO can significantly reduce the amount of natural gas it burns to produce steam for EOR. 
<br />
<br />
GlassPoint’s unique Enclosed Trough design encloses parabolic mirrors inside a glasshouse structure, protecting the solar collectors from harsh conditions of high wind, dust, dirt, sand and humidity common to Middle East oilfields. The glasshouse enclosure enables the use of ultra-light, low-cost reflective materials and proven automated washing equipment, further reducing costs. GlassPoint steam generators are designed to use the same low-quality boiler water as once-through steam generators, the industry’s current standard, eliminating the need for costly water pretreatment.
<br />
<br />
<b>Promoting renewables
</b>
<br />
<br />
Meanwhile, Germany has been proactive on the renewable energy front in the Sultanate thanks to numerous efforts undertaken by the embassy as well as the trade arm of the country. It is well known all over the world that Germany paved the legal way for a “Turnaround in Energy” and is the first industrial nation which has decided to quit energy supply from nuclear power by a fixed date namely 2022.
<br />
<br />
As far as government-to-government collaboration and involvement by the embassy, the initial steps to foster bilateral cooperation between the two governments date back a few years, says Gerhard Truemper, Second Secretary (Commercial), German Embassy, Muscat. The government owned technical agency German International Cooperation (GIZ) has been cooperating with The Research Council of Oman (TRC) in the field of energy efficiency and the use of renewable energies. The German Federal Ministry for the Environment and Oman’s Public Authority for Electricity and Water (PAEW) are in close contact to identify areas of cooperation for mutual benefit.
<br />
<br />
There have been two exciting projects on the new GUtech-Campus in Halban. The university campus is the first “green” campus in Oman. The Eco-House, which serves as a guesthouse for visitors from abroad to teach and research at the University, uses radiant cooling instead of regular airconditioners. It also generates its energy through photovoltaic panels, has its own water recycling system and a small garden with local plants.
<br />
<br />
The Eco-House is developed by a group of 30 students and their professors of architecture in GUtech, as well as the Omani-German achitecture consultants Hoehler & Partner, the contractor Larsen & Toubro, BAUER Water GmbH and Muscat Municipality. The Eco-House competition was sponsored by the The Research Council.
<br />
<br />

According to Simone Lisker, representative, German Industry and Commerce Office (AHK Oman), Muscat, says that as a result of the various studies which were presented in 2008/2009 and showed the immense potential in Oman primarily in the solar energy sector and following the huge political interest in the Sultanate to take a pioneer role in the region for the development of “Renewable Energies” many German companies have been interested in discovering the Omani market.
<br />
<br />
In May 2010, a German company – Azur Space Solar Power GmbH – launched a 3G solar panel in Knowledge Oasis Muscat - the first of its kind in the Middle East while another leading German company, -- Phoenix Solar AG – established a subsidiary in Muscat called Phoenix Solar– as a joint venture with Silver Circle being their local partner.
<br />
<br />
This German/Omani joint venture was entrusted to design one of the six pilot projects which were confirmed by the AER In 2010 - a 28 KW solar power facility in Al Mathfa based on the BOO-concept (Build-Own-Operate).

<br />
<br />
DONAUER, one of the leading photovoltaic-system suppliers in Germany established their subsidiary Donauer Ali Badi Gulf in Muscat in April 2012 as a joint venture. According to Lisker, they are convinced about the future of photovoltaic in Oman and have developed innovative solutions (Diesel/Photovoltaic-Hybrid System). They are currently negotiating to implement pilot-projects in this field and participated in a tender by Majan Electricity.
<br />
<br />
All in all, we can say that the spark has been lit for renewable energy and it will only be a matter of time before the blaze burns bright and blithe.&#8194;
