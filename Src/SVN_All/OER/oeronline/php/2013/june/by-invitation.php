	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; line-height:17px"> 
				<b><i><font face="Verdana" size="2">This is the first of a two-part article discussing the peculiarities and nuances unique to Islamic accounting </font></i>
</b></p>
                </td>
		</tr>
	<tr>
