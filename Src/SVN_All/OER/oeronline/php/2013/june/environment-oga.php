						<font face="Verdana" size="4" color="#C00000">Preserving Cloud Forests 
</font></b></p>
						<p align="justify" style="margin: 3px 10px; line-height:17px"><b><font face="Verdana" size="2">
                		<br>
Environment Society of Oman plants native trees with $27,500 grant from Ford Motor Company to preserve the unique cloud forests in Dhofar <br /></font></b>
<p align="justify" style="margin: 3px 10px; line-height:14px">
						<font face="Verdana" size="1">
 
With more than 900 plant species, including over 50 endemic ones not available anywhere else on Earth, the mountains of the Dhofar region in Southern Oman are a biodiversity hotspot of unique value. Every summer, while the rest of the Arabian Peninsula is scorched by the heat, the slopes around Salalah turn green due to moisture from the Indian Ocean monsoon, locally known as Khareef.
The unique cloud forests, sustained by the monsoon, have supported generations of traditional herders in Dhofar. But over the past four decades a rapid increase in population has driven up the pressure with the forests now increasingly turning into grasslands. Overgrazing by camels and other domestic animals, as well as the rapid development of infrastructure, are the main causes for the disappearance of trees, said Dr Mehdi Ahmed Jaaffar, vice president, Environment Society of Oman (ESO), which is working to protect the forests.
“People tell us that, less than 20 years ago, they could have easily hidden their herds of camels in the woodlands,” he said. At present, in comparison, the hills are “barren” in many places, he said.

<br />
<br />
 With the help of the Ford Motor Company Conservation and Environmental Grants and other donors, ESO is working to educate people about the value of the forests and the ways to protect them.
<br />
<br />
 In 2012, ESO became one of nine recipients of funding under the Ford Motor Company Conservation and Environmental grants scheme. Altogether $100,000 were given to environmental projects in Jordan, Lebanon, Iraq, the UAE and Oman, with ESO receiving US27,500 in funding. The Ford grants scheme, which has given out a total of $1.3mn since 2000 to environmental organisations in the region, is open to participants from the UAE, Bahrain, Kuwait, Oman, Qatar, Saudi Arabia, Lebanon, Jordan and Iraq. The programme is accepting applications for funding via Ford Middle East’s Facebook page until 1 July 2013.
<br />
<br />
 “The Grants made us feel confident that we were on the right track,” says Dr Jaaffar.
<br />
<br />
 It also provided the funding to cover the logistics and the cost of printing educational materials for the “Let’s Plant One” tree-planting campaign, which ESO just concluded in Dhofar.
<br />
<br />
“The campaign aims at developing awareness of the importance of indigenous trees and the role they have played throughout history,” says Dr Jaaffar. “The idea is to leave behind education and awareness of the need for different parties to take this issue as seriously as we would like them to.” 
<br />
<br />
Over five days starting from March 31, ESO planted native trees, such as Boswellia sacra, commonly known as the frankincense tree, at several locations, including Dhofar University, the Jebel Samhan’s Nature Reserve Visitors’ Centre, the Deputy Wali’s Office as well as schools in Rakhyut, Dhalkut and Rabkoot. Educational talks and competitions were also held.&#8194;
