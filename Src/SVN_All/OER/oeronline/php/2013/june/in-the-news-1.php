					<b><font face="Verdana" size="4" color="#C00000">Building blocks of development</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Assarain & Modern Concrete Products have launched some unique and innovative construction and landscaping products</font></b>
						
						<br />
						<center>
						<img src="images/inthenews_2.jpg" border="0">
						</center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
Muscat-based Assarain Concrete Products & Trading (ACP) and Sohar-based Modern Concrete Products (MCP) jointly introduced some cost effective and latest construction and landscaping solutions for the construction industry, recently at the Grand Hyatt, Muscat. The event was presided over by HE Eng Salim bin Mohammed Al Nuaimi, Under Secretary, Ministry of Transport & Communication who unveiled and launched the new products. Over 400 guests including dignitaries and government and corporate representatives attended the event. 
<br />
<br />
Displayed on stage were the latest products, set to benefit the commercial sector and the general public alike, ranging from latest shotblasted pavers, split-block wall systems, flower walls, new designs of interlocking tiles to the latest lightweight autoclaved aerated concrete (AAC) blocks, rubber tiles, flooring and sport tracks and most fascinating LED-lit interlocking pavers.
<br />
<br />
 Assarain & Modern Concrete Products have already grown multifold in the last decade. It was noted that ACP & MCP combined have the highest market share as they possess an innovative range of products and designs and many useful smart construction and landscaping products too. Launching the latest products in the market, ACP, in its core competency area, introduced some high-quality shotblasted tile finishes for the first time in Oman. This is backed by highly advanced surface finishing plants at their Rusayl complex. During the launch, the entire construction industry got to see new generation boundary wall system wherein significant savings can be made as there is no requirement of plastering and painting. The split block wall systems and the flower wall systems were introduced with wonderful antique finish providing great exterior aesthetic looks and that too with significant cost savings in building boundary walls.
“As a dedicated team, we remain committed as a partner to the construction industry, towards creating a beautiful Oman and shall continue to fulfill our pledge to our customer base by way of bringing in beneficial and novel products to them,” said 

<br />
<br />
 Khalid Said Al Wahaibi, managing director. “As Oman has been witnessing continuous, all-inclusive growth under His Majesty’s wise and able leadership, Assarain realises it’s role and would thus continue to contribute by way of providing quality and variety products and continue to retain the trust and faith its customers have reposed in us.”
<br />
<br />
 A R Varma, general manager, informed that shotblasted tiles provide much greater aesthetic edge over normal paving tiles and were widely liked by one and all. “Even for the fast moving types of interlocking tiles, a slew of latest designs were now available, which were very carefully chosen by our team and then launched. The choice of colours, design and combination indeed provides many new alternatives to the entire construction industry. It also provides an alternative to move away from the routine designs as most of the customers look for variety and new designs, that too, delivered by us at the same competitive prices.”&#8194;
 
