					<b><font face="Verdana" size="4" color="#C00000">UMS to publish Bloomberg Businessweek Middle East</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font>
						<br />
						
						<center><img src="images/pub_1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">
 United Media Services International (UMS) and Bloomberg Businessweek have announced their agreement to publish English and Arabic editions of Bloomberg Businessweek Middle East.
 
<br />
<br />
 This partnership will allow Bloomberg Businessweek to continue to expand its reach among globally-minded business leaders in one of the most important and emergent economies in the world. The agreement also reinforces UMS’s leadership position as one of the region’s most dominant publishing, advertising, public relations and media distribution companies.
<br />
<br />
 Launch preparations are currently underway for the English edition of Bloomberg Businessweek Middle East which will be published twice per month, and a first issue date will be announced soon. The targeted circulation of the magazine is 24,000, focused on an audience of business leaders in Bahrain, Egypt, Iraq, Jordan, Kuwait, Lebanon, Libya, Oman, Qatar, Saudi Arabia, and the United Arab Emirates. The Arabic edition of Bloomberg Businessweek will also be published twice per month and is scheduled to launch in May 2014.
<br />
<br />
 Bloomberg Businessweek Middle East will provide readers with incisive analysis and unparalleled access to the CEOs, dealmakers, and industrial decision makers around the world who are changing the face of business and finance. Both editions will feature the best global content from Bloomberg Businessweek, which draws on Bloomberg’s 2,400 journalists worldwide, and locally focused business and financial content produced by UMS’s vast regional network of bureaus and editors.
<br />
<br />
 UMS has publishing centres in the United Arab Emirates, Qatar, Oman and Bahrain, and a portfolio of 17 print titles including such leading magazines as Oman Economic Review, Qatar Today, GLAM, Signature and the UAE and Qatar editions of T: The New York Times Style Magazine.
<br />
<br />
 “We are excited and proud to be publishing the Middle East edition of Bloomberg Businessweek,” said Sandeep Sehgal, chief executive of UMS. “Our research clearly indicates that there is space for a high quality business title which covers the region completely and equitably, and we are confident that Bloomberg Businessweek Middle East will grab the attention of eager executives.”
<br />
<br />
 “We are very pleased to be working with UMS, and look forward to producing Middle East editions that offer dynamic, timely business content about the local and international marketplace,” said Bloomberg Businessweek president Paul Bascobert. “We know Bloomberg Businessweek Middle East will uphold the high standards of the Bloomberg Businessweek global brand, and that advertisers will consider the magazine an important channel to the influential audience of business and financial professionals in this region.”
