					<b><font face="Verdana" size="4" color="#C00000">In full swing</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The National team is confident about putting up a good performance at the upcoming Arab Amateur Golf Championship in Egypt. Azzan Al Rumhy and Marcus Casey share their thoughts with OER</font></b>
						<br />
						
						<center><img src="images/oman_golf_1.jpg" border="0"></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" class="style13">
 
Oman golf committee (OGC) will field a four-member national team in the upcoming 33rd Arab Amateur Golf Championship in Egypt from June 23-29. It’s one of the most prominent regional tournaments and the team is getting ready to perform at their best. <br />
<br />
<br />
 Azzan Al Rumhy, Oman´s most succesful player says, “Individually, I would definitely like to improve on last year’s position by targeting a top 10 finish. With regards to the team, I am very confident that we will improve on our last year’s position and hopefully make up for the disappointment of the GCC event. We hope to field our best team for the event. Having National coach Marcus Casey join us for the trip will definitely help us prepare well before and during the event. We are all very excited about the tournament and hope we finish better than our best ever eighth position as a team.”
<br />
<br />
 On the achievement in the 32nd Pan Arab Championship, Azzan says: “As a team we finished ninth out of 13 countries that participated. I did pretty well individually as I finished 12th out of 47 players that participated We missed two of our experienced national team players last year as both Ali Hameed and Mohammed Al Mawly couldn’t participate for personal reasons.”
<br />
<br />
On his own game Azzan says; “I feel more comfortable with some of the changes in my swing I worked on improving with Marcus before the GCC event. My putting which let me down a lot during the GCC has improved which has lowered my scores since February. I have managed to win two major open events at two different clubs that are not my ‘Home’ clubs after the GCC which has improved my confidence. Winning the 4th Muscat Open in early March at Muscat Hills right after the GCC followed by the Back Tee Challenge at Al Mouj will definitely help me ahead of the upcoming Pan Arab event in Egypt which is happening at the end of June. I shot my best score at GVGC yesterday (22nd of May) of 33 (3 under par) in a GVGC 9 hole match play event which proved to me that I am capable of playing very well when my game is in top form.” 
<br />
<br />
Training and prepartaions before a major competition is crucial and Marcus Casey, Almouj Golf pro and coach for the National team explains the process; “The way to prepare the team for a major championship starts well in advance and is holistic in its approach. The main area of focus will be on the players short game skills. Skills such as bunker shots and chipping and putting. These are parts of the game that can be improved very quickly and without much disruption to the players overall technique. The short game is the one area which can help a player no matter how well or bad they are playing. 
<br />
<br />
Preparation is key says Azzan: “It’s important to make sure that the team prepare themselves well ahead before an important national team event such as the Pan Arab. Marcus has always emphasised the importance of working hard on improving our physical condition by giving us various types of exercises and drills to work on. We will all work hard to be as fit as we can possibly be ahead of the Pan Arab event by keeping a close eye on our diet as well! We all realise how important the short game is to shoot low scores, so I expect our focus will be on improving our short game during the next two-to-three weeks.”
<br />
<br />
Mental attitude and preparation for each round and even each shot is of great importance, says Marcus, “Getting each player to mentally perform each day to their maximum potential will assist and help them to shoot good scores. Again this is an area, in which a little guidance and assistance can lead to great results with little disruption to their individual techniques. There is only six weeks before we depart for Egypt and in that period we can only assist each player to perform their best for that week. Eating for performance needs to start well in advance of the competition and thus what and when the players eat and drink will be monitored.”
<br />
<br />
Marcus concludes; “As the national team coach I will prepare the team to the best of their ability within the time constraints leading up to the event. The team will be well prepared and will give their best effort on each day. Should all the team members perform well, a top five finish is not out of the question. My major expectation for the week is for us as a team to upset a few of the favourites. I see a great future in the National team and believe that they could cause an upset soon.”
<br />
<br />
The national team will be accompanied by board members of the Oman Golf Committee to Egypt. The Oman Golf Committee was set up in January 2009, following a ministerial decision from the ministry of sports affairs. It is tasked to manage golf as a sport from all technical, practical, financial and organisational aspects in the Sultanate or abroad.&#8194;
