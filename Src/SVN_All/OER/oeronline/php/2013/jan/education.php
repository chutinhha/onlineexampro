						<p align="justify" style="margin: 3px 10px">
				<b>
				<font face="Verdana" size="4" color="#C00000">Investing in 
				tomorrow</font></b><p align="justify" class="style13">
				<b>The fourth edition of Muscat Youth Summit offered an 
				extraordinary opportunity for talented youth in the country to 
				come together and learn how to channel their passion into 
				opportunities for future. An OER report</b><p align="center" class="style13">
				<br>
				<img border="0" src="images/education-jan-13-1.jpg" width="300" height="288" hspace="12" vspace="3"><p align="justify" class="style13">
				As many as 200 students returned to their schools, colleges and 
				universities with new inspiration and an insight into industry 
				and entrepreneurship as Muscat Youth Summit 2012 concluded. Over 
				the course of the four day residential programme, participants 
				from 26 countries attended interactive workshops and community 
				projects that stirred their imaginations and opened their eyes 
				to new career possibilities.<br>
				<br>
				HH Sayyid Faisal Al Said, chairperson of the Muscat Youth Summit 
				organising committee, emphasised the Summit’s role as a key 
				facilitator in developing the next generation of business owners 
				and community leaders. He said, “The Summit is an important 
				platform for developing the youth of Oman by means of promoting 
				innovation and enterprise. It also provides a unique opportunity 
				for participants to meet entrepreneurs who have turned ideas 
				into successful businesses.”<br>
				He added, “With such a young population, creating commercial 
				opportunities is key to ensuring Oman’s long-term and 
				sustainable economic growth. Through the Muscat Youth Summit, we 
				were able to expose participants to emerging business trends and 
				open their eyes to new ideas in areas such as energy, health, 
				transportation, media, entertainment and tourism.”<br>
				<br>
				<b>Promoting entrepreneurial skills<br>
				</b>The first day of the summit focused on the main approaches 
				to promoting entrepreneurial skills. Participants learned how to 
				develop their idea and the basics of creating and running a 
				startup business, managing their resources and premises, and 
				gain experiential advice from professional mentors. The second 
				day of the summit highlighted on equipping the students with new 
				potential business ideas through a series of interactive 
				workshops that inspired and challenged their creativity. 
				Focusing on growing industries such as fashion and small batch 
				manufacturing as well as social and virtual initiatives to 
				promote road safety, students enjoyed a hands-on approach to 
				identifying market trends, high growth businesses and how 
				technology can benefit their entrepreneurial activities.<br>
				<br>
				The third day of the summit saw the culmination of innovation, 
				imagination and inspiration as students exhibited their work 
				after two days of interactive workshops that ignited their 
				creative spark. Sessions focusing on social enterprise, urban 
				art, road traffic safety and the fashion industry explored small 
				business production and how to utilise technology in concept 
				identification and delivery of entrepreneurial projects.<br>
				<br>
				Learning more about safer road design and how to create a 
				non-motorised community, students visited The Wave Muscat, to 
				speak with residents and assess the infrastructure and 
				environment as part of the workshop entitled ‘Safe Streets: 
				Share The Road’. Aimed at developing mutual respect between all 
				road users, students submitted an action plan with targets and 
				timelines for a pilot programme to encourage cycling and walking 
				as alternative modes of transportation in Muscat.<br>
				<br>
				<b>Journalism training<br>
				</b>Students also learnt the fundamentals of journalism as they 
				produced their own magazine. Beginning with the idea and target 
				audiences, the groups created enticing headlines and attractive 
				page layouts as well as established a business plan and 
				potential advertisers to make the magazine a success. Mentored 
				by publishing professionals from the UK’s leading youth 
				magazine, ‘Live Magazine’, participants were given an insight 
				into how to manage a leading publication as they worked together 
				to develop the content for the MYS 2012 magazine and social 
				networks.<br>
				<br>
				Organised by the Public Authority for Investment Promotion and 
				Export Development (PAIPED), the Summit’s workshops helped 
				instill confidence, nurture critical life-skills and highlighted 
				the diverse talents required to succeed. To that end, students 
				learned how to fine-tune a business concept, develop a business 
				plan, build a network of contacts, partner with investors and 
				market their idea. The summit came to an end as participants and 
				organisers joined hands in the spruced-up Al Mussanah Public 
				Park. Commenting on his participation in the 2012 Summit, Ahmed 
				Al Busaidy, a student from the Higher College of Technology 
				said, “The knowledge and friendships I gained over the four days 
				of the summit will remain with me for the rest of my life. I 
				realise now the responsibility I have towards my country and 
				have a clear idea of what I want to do with my future.” <br>
				<br>
				The Muscat Youth Summit 2012 was supported by the Diwan of the 
				Royal Court; Ministry of Education; UNESCO; Oman National 
				Commission for Education Culture and Science; The National Youth 
				Committee; The Young Foundation; OMIFCO; Nawras; Khimji Ramdas; 
				Oman Air; Omran; Qalhat LNG; The Oman Observer; Oman; 
				Merge104.8; Al Wisal FM; Millennium Resort Mussanah; and the 
				Grand Mall Muscat.&#8194;</td>
