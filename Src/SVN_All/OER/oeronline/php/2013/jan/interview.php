						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>In leadership mode</strong></span><p class="style15">
				<span class="style14"><strong>Salaam Al Shaksy, CEO, National 
				Bank of Oman shares his views on the bank’s achievements and the 
				roadmap for future in a candid chat with Mayank Singh</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-jan-13-1.jpg" style="border: 1px solid #808080; " width="400" height="266"></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<b>Looking back on the year 2012, what are your thoughts on 
				NBO’s performance, challenges and opportunities?</b><br>
				The year 2012 has been a good one for the National Bank of Oman; 
				yet as is often the case in business, there were challenges to 
				tackle and overcome. Our retail banking business has taken off 
				and established itself as a leader in that market; however, in 
				wholesale banking, as a result of stiff competition, shrinking 
				margins have affected the banking sector as a whole and NBO is 
				no exception. In addition to this, regulatory changes in retail 
				banking took effect mid-year which has resulted in further 
				reducing profitability.<br>
				<br>
				Overall, I am pleased with what we have achieved in 2012. We won 
				the Bank of the Year award amongst further recognition by 
				various other bodies. NBO is continuing to regain its leadership 
				position in the sector, although I feel that there is more that 
				can be done as there is always scope for further improvement in 
				a highly competitive market.<br>
				<br>
				<b>You mentioned the success of NBO’s retail business-were there 
				certain initiatives that helped you to do well in retail 
				banking?</b><br>
				On the retail banking side we have undertaken a number of 
				strategic and operational initiatives, including an enhanced 
				product offering, improved delivery channels and superior 
				customer service in order to offer an ever improving experience 
				to our customers.<br>
				<br>
				Another key business driver that has been, and continues to be 
				addressed, is recruitment and employee development. We worked 
				hard on attracting good talent and I am proud to say that many 
				of them are Omanis. We have ensured a clear and effective 
				communication to the team on our vision and strategy, which has 
				enabled our people to take ownership and pride in where we want 
				to go and how we want to get there. NBO’s retail banking 
				division turned around rapidly because of our people, a shared 
				vision, our focus on the customer and the fact that we were 
				market driven. These were and continue to be critical to the 
				success of retail banking.<br>
				<br>
				<b>Changes in the regulatory framework and shrinking margins 
				were challenges – did these affect your performance or would you 
				have done better?</b><br>
				Globally things have changed as dollars are not as readily 
				available as they previously were and the cost to banks has 
				increased drastically. Some large quasi-government entities or 
				companies have shifted from dollar borrowing and moved to Rial 
				Omani, at very low pricing. The hope is that going forward this 
				issue will resolve itself. As more banks enter the market, 
				margin pressure is going to be a continuing challenge, but I 
				hope that all of us will start interacting sensibly and ensure 
				that there is healthy rather than destructive competition.<br>
				<br>
				<b>The Central Bank of Oman has changed the interest rate 
				structure charged by banks; will this affect the profitability 
				of the banks like NBO?</b><br>
				I believe the new regulations on consumer lending will bring 
				much needed long term benefits to the economy. However, in the 
				short-to-medium-term, there will be an impact on the 
				profitability of the banking sector’s retail business. Also, the 
				effect of the interest rate reduction by one per cent has 
				started to be felt as loans come up for renewal and new loans 
				get disbursed. It is now imperative that banks shift their focus 
				towards more constructive forms of lending, which would include 
				mortgage and SME loans.<br>
				<br>
				<b>A lot has been said about Islamic banking and its prospects. 
				Is NBO prepared to commence its Islamic banking operations and 
				what kind of an opportunity does it present for banks in Oman?</b><br>
				NBO is certainly ready; we have an Islamic window which is ready 
				to be launched, offering a range of Sharia’a compliant products 
				under the brand name of “Muzn” which has already been launched. 
				Our Islamic banking branch and our people are ready.<br>
				<br>
				I am not sure whether Islamic banking will necessarily create 
				significant new opportunities in the banking sector, but it will 
				certainly help us in substituting some conventional products, 
				for customers to do their financial transactions and borrowings 
				in a Sharia’a compliant manner. It is also claimed that 
				significant funds were deposited outside the country in search 
				of Sharia’a compliant offerings. These funds can now return and 
				be put to good use to serve the further development of our 
				nation.<br>
				<br>
				<b>What are NBO’s priorities for 2013 and what are your thoughts 
				on the coming year?<br>
				</b>Our priority for 2013 is to diversify our revenue streams. 
				We are looking at expanding our UAE franchise in that market. In 
				addition, we will be pursuing other strategies: the bank is 
				looking at strengthening its mortgage proposition and its 
				offerings in the SME segment as well. We will continue to work 
				hard to deliver sustainable results for 2013 and beyond.</td>
