					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">World Wide 
					Web is meant for keeping the world more diverse and exciting 
					by decentralising knowledge and making data accessible to as 
					many people as possible, states its iconic originator Sir 
					Tim Berners-Lee at a lecture in Sultan Qaboos University. 
					Muhammed Nafie reports</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/Technology-jan-13-1.jpg" style="border-style: solid; border-color: #808080; " width="400" height="253"></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				When the concept of World Wide Web was first introduced in 
				December 1989, who could have guessed that it would explode the 
				floodgates of linked data turning our life inside out? After 
				twenty three years of the so-called data revolution, we still 
				wonder where it will take us in the next couple of decades. But 
				its iconic architect has no worries about the objective and 
				mission of his invention. “You should use the web technology to 
				keep the world more diverse and exciting,” stated Sir Tim 
				Berners-Lee addressing a packed audience comprising students, 
				academics and professionals at the Sultan Qaboos University 
				Cultural Centre. “The job of the Internet is to be transparent 
				and be a means to an end-to-end system. If we use the Web we 
				should preserve its properties and in doing so make the world a 
				diverse and exciting place.”<br>
				<br>
				In a speech which blended his childhood memories and his 
				evolution as the inventor of Web with his insightful perspective 
				on the future of technology, Sir Berners-Lee called for an open 
				data policy on the Web which will encourage people to share data 
				for information and research. Observing that the process of 
				linking data which requires only a limited amount of energy 
				results in larger benefits for the society, he said, “We have to 
				value the importance of linking data as it allows one to work or 
				learn from a third person who might be an unknown entity.”<br>
				<br>
				Referring to the spirit of decentralisation involved in the 
				evolution of Internet from 1969, Sir Berners-Lee asked the 
				students to keep their designs layered and open-ended so that 
				new generation can build on it in the future. “When the internet 
				was first developed in 1969 it was layered so that 20 years 
				after that I could develop web on that and now after twenty 
				years you can design things on top of the web. When you design, 
				design it for people who are to come in the future and who can 
				build things that you cannot imagine. Design it in a way that it 
				can be used in many ways later.” A strong advocate for openness 
				and free accessibility of data to all, Sir Berners-Lee believes 
				that it is essential to make one’s web project accessible to as 
				many people as possible. “Web is designed to be accessible to 
				people with disabilities,” he said explaining that using audio 
				features and captions in the websites is an effective way to 
				reach out to differently abled people.<br>
				<br>
				<b>Oman’s future<br>
				</b>Talking about Web Index, world’s first multi-dimensional 
				measure of the Web’s growth, utility and impact on people and 
				nations, he said, “The index shows how best the Web is being 
				used and what its social impact is. It covers 61 developed and 
				developing countries. Although Oman is not part of it, I hope 
				that by next year it can provide information and be on the 
				list.”<br>
				He opined that Oman needs to develop a process of interlinking 
				data from different sources. He exuded confidence that linked 
				data with various classifications (for example the details of 
				hospitals, teachers and universities etc) will usher in a new 
				era of World Wide Web in Oman. “It allows you to pick your 
				battles, pick where your energy will be spent,” he said. 
				Addressing the problem of people’s reluctance to provide 
				information, he said giving credit for any information used and 
				honouring people for their share of work will encourage more 
				people to share information.&#8194;</td>
