						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Commercial Agencies Law<br>
						</font><font face="Verdana" size="2">
                		<br>
						In Oman an overseas arbitral judgment can be rendered 
						null and void if it contains something which contravenes 
						Omani public policy</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
						<i><font face="Verdana" size="1">By James Harbridge</font></i><b><font face="Verdana" size="2"><br>
						</font></b><font face="Verdana" size="1"><br>
						&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="http://www.oeronline.com/images/James-Harbridge.jpg" width="250" height="185"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">
						<br>In the October issue, I explained that the Omani 
						Courts are very likely to decline jurisdiction to hear 
						an agency dispute if the contract in question refers to 
						arbitration. This means that if an agency refers to 
						arbitration and a foreign law, the likely outcome is 
						that the arbitral panel will apply that foreign law and 
						exclude Omani law.<br>
						<br>
						However, there remains the possibility that an arbitral 
						panel may be persuaded by the agent that Oman’s 
						Commercial Agencies Law (“CAL”) is a mandatory local law 
						which has to be considered, notwithstanding the fact 
						that the parties have agreed to a non-Omani governing 
						law.<br>
						<br>
						Furthermore, although Oman is a signatory to the New 
						York Convention on the recognition of foreign arbitral 
						awards, it is noteworthy that Oman’s Royal Decree 47/97 
						– the Arbitration Law - states in its Article 53 that an 
						overseas arbitral judgment can be rendered null and void 
						if it contains something which contravenes Omani public 
						policy.<br>
						<br>
						It is therefore potentially possible that the Oman 
						Courts may take jurisdiction, and apply Oman law, if an 
						aggrieved agent provided evidence that it had first 
						complied with a non-Omani arbitration which applied 
						non-Omani law, with the consequence that the agent was 
						‘’deprived” of the benefits which would have accrued to 
						him if the dispute had been heard under Omani 
						substantive law.<br>
						<br>
						It would also therefore be interesting to see how the 
						Oman Courts would react if an aggrieved Omani agent 
						argued that an arbitral award should be nullified since 
						the award applied agency laws of another country other 
						than those of Oman. As yet, I have no knowledge of such 
						a scenario occurring.<br>
						<br>
						<b>Registration<br>
						</b>According to Article 11(b) of CAL, every agency 
						contract in respect of Oman must be registered at Oman’s 
						Ministry of Commerce &amp; Industry (and any contractual 
						agreement trying to avoid this obligation would be 
						unenforceable under Omanilaw). Moreover, Article 19 of 
						CAL criminalises any violation in this regard, imposing 
						fines and ultimately granting the ability for the 
						authorities to shut down that part of the agent’s 
						business relating to the subject agency. To the best of 
						my knowledge, the reality is that the Article 19 
						sanctions have never actually been imposed in Oman.<br>
						<br>
						In practice, many agency contracts are actually 
						unregistered, or else they begin life as registered 
						agencies, but in later years the registration expires 
						and there is a failure to renew the registration.<br>
						<br>
						The significance of registration lies primarily in the 
						fact that Article 11(d) of CAL states, “Any agency which 
						is not registered shall not be recognised and any case 
						concerning it shall not be heard.”<br>
						<br>
						Accordingly, many foreign principals who are defendants 
						in Omani Court cases brought by agents will argue that 
						the Court should dismiss the case owing to lack of 
						registration.<br>
						However, this line of argument is unlikely to succeed 
						where the agent can prove that registration failed to 
						take place owing to the foreign principal’s 
						failure/refusal to put the agency agreement through the 
						notarisation, legalisation, endorsement and 
						authentication procedures requested by the Ministry.<br>
						<br>
						Equally, an agent who is a defendant in an Omani case is 
						unlikely to escape liability by running the argument 
						that it has no obligations to the foreign principal 
						because the agency was unregistered. The Supreme Court – 
						Oman’s third and final tier of justice – recently ruled 
						that an agent who failed to pay for goods delivered by 
						the foreign principal must of course still pay for those 
						goods, regardless of the fact that the relevant agency 
						had never been registered.<br>
						<br>
						On the other hand, in a different case where the 
						Claimant was a former Omani agent, Oman’s Primary Court 
						ruled that the agency was registered even though it had 
						in fact been unregistered for at least five consecutive 
						years prior to the agent receiving notice of 
						non-renewal. This case was never appealed so it may not 
						be followed by Oman’s judiciary in future cases. The 
						Primary Court in that case appeared to be saying that it 
						would treat the agency as registered since at some time 
						in the past it had indeed been the subject of a valid 
						registration certificate.&#8194;</font></td>
