						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Trend setters<br>
						</font><font face="Verdana" size="2">
                		<br>
						Experts at Arab Aviation and Media Summit 2012 envisage 
						an enormous potential for growth for low-cost airlines 
						in the Middle East, capitalising on robust demand. 
						Muhammed Nafie reports from Sharjah</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/aviation-june-12-1.jpg" hspace="8" vspace="5" width="500" height="334"><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">Low cost carriers – or 
						budget airlines – appear to be ruling the skies today 
						globally at a time when world’s biggest airlines are 
						feeling the sharp pinch of financial crisis and rising 
						fuel prices. The big industry carriers – or so called 
						legacy carriers – are now making room for low cost 
						airlines, that are emerging profitable, broadening their 
						reach and entering new markets. Although the Middle East 
						remains one of the least penetrated markets by the 
						low-cost carriers, as the aviation stocks in the region 
						are dominated by state carriers, the region has a 
						growing number of local low cost airlines, operating 
						with great efficiency and success. They continue to play 
						a key role in increasing air traffic and creating their 
						own markets.<br>
						<br>
						Highlighting an enormous potential for growth for 
						low-cost airlines in the region, capitalising on robust 
						demand on both regional and international markets, was 
						the Arab Aviation and Media Summit 2012 organised 
						recently at the American University of Sharjah in the 
						UAE.<br>
						<br>
						“Low-cost airlines have evolved into an innovative and 
						sustainable business model over the last few years, 
						dispelling the misconception that they compromise on 
						quality and service,” said Adel Ali, CEO of Air Arabia, 
						Middle East’s first low-cost, low-fare airline emerged 
						in 2003. “The success of low-cost airlines once again 
						underlines the fact that passengers opt for them. The 
						Arab spring has negatively impacted on the regional 
						industry with massive plunge in passenger numbers though 
						the sector has shown considerable resilience in the face 
						of the crisis.”<br>
						<br>
						It was Air Arabia, the first airline to be listed in the 
						Middle East, who pioneered the low-fare concept in the 
						region. The Sharjah-based budget carrier, which 
						currently operates out of the two other hubs in Egypt 
						and Morocco, flies to 74 destinations. With a fleet of 
						30 aircraft, Air Arabia has been successfully expanding 
						its reach in the region and beyond to markets in Europe 
						and Russia. It started with a pair of Airbus A320s, but 
						intends to grow this to 50 by 2015. Speaking at the 
						sidelines of the summit, Adel Ali informed that the 
						carrier posted a net profit of Dh274mn during the year 
						2011 and its first quarter net profit in 2012 was up 11 
						per cent to Dh49.2mn compared to the corresponding 
						period in 2011.<br>
						<br>
						Chris Taylor, vice president, Spectrum Capital, UK, 
						praised the dynamism of Middle Eastern airline operators 
						pointing out there was ample opportunity for growth, 
						especially in the low cost sector. “Everybody is buying 
						into the concept of low-cost carrier,” he said. “Even 
						the legacy carriers are setting up low cost brands. For 
						example Singapore Airlines has a low cost brand and 
						Iberia Airlines has also set up a low cost brand. It is 
						the same airline but is marketed as a low carrier. The 
						low-cost carriers with their low fares have reduced the 
						cost of travel. There are now many variations on the 
						basic model with ancillary income being generated from 
						sales of additional services at the time of booking and 
						also while on board the aircraft.”<br>
						<br>
						<b>Broader open skies policy<br>
						</b>The industry experts opined in the summit that in 
						order to unleash the full potential of low-cost carriers 
						in the region, the Arab aviation industry was eyeing a 
						roadmap for a broader open skies policy in the region. 
						Adil averred that the industry’s growth potential had 
						underperformed due to the limited progress on open skies 
						agreements. “The regional governments are facing a 
						dilemma whether to open up their skies to other airlines 
						or continue to protect their flag carriers and incur 
						huge losses”, he said. “Nationalistic sentiments and 
						fear of competition have hampered the pace of progress. 
						While the UAE, Lebanon, Jordan and Morocco have 
						encouraged liberalisation of their aviation markets 
						through open skies agreements, several other countries 
						in the Middle East are still holding back on adopting a 
						full open sky policy.” He also said as regulatory 
						barriers to entry go down, passengers had been the 
						beneficiaries with an increasing number of carriers 
						resulting in a more competitive market, with lower fares 
						and increased focus on customer satisfaction. “The 
						tangible benefits of a liberalised market also include a 
						higher rate of job creation. Across the aviation sector, 
						more employment opportunities arise from the servicing, 
						management and maintenance of additional air services,” 
						he added.<br>
						<br>
						Abdul Wahab Teffaha, secretary general of Arab Air 
						Carriers Organisation (AACO), said, “Although all the 
						governments in the region have made commitments to 
						liberalise their aviation industry, only eight countries 
						have signed the ‘open skies policy’ convention. AACO 
						will demand speedy implementation of the commitments 
						made by the Arab countries within the framework of the 
						Damascus convention at the next meeting of Arab Civil 
						Aviation Commission. The Arab aviation industry is a 
						catalyst for growth provided the governments lift the 
						regulatory regime and make a liberalised environment for 
						airlines to operate.”<br>
						<br>
						<b>Challenges and opportunities<br>
						</b>However rising fuel costs will continue to be a 
						burden for low-cost airlines. As fuel prices continue to 
						be a wild card in 2012 with crude oil hitting $125 a 
						barrel recently, low-cost airlines are expected to 
						struggle to keep their stock afloat. In addition, 
						continuing capacity expansion by big players will only 
						add to the competitive pressure of smaller entities.<br>
						<br>
						Experts opined that though crisis had affected tourism 
						industry in countries such as Egypt, Tunisia and Yemen 
						registering a 50 percent drop in tourism arrivals, the 
						industry had bounced back significantly in the last few 
						months. “Passenger numbers on Middle East airlines rose 
						by 8.9 per cent in 2011,” said Dr Majdi Sabri, regional 
						vice-president MENA, IATA. “In the first quarter of 
						2011, the industry recorded a 15 percent jump in net 
						sales.”<br>
						<br>
						“Arab Spring not only presented challenges but enormous 
						opportunities to the industry,” said Abdul Wahab. “In 
						the Middle East alone, the aviation sector currently 
						supports 2.7 million jobs and accounts for $129bn in GDP 
						approximately. Over the next two decades, Middle East 
						international passenger numbers are expected to reach 
						some 220 million in 2030 as compared to 77 million in 
						2010. Regional markets derive substantial economic 
						benefits from the spending of tourists travelling by 
						air.”<br>
						<br>
						Predicting a slow recovery in the regional aviation 
						sector, Chris Taylor added, “In the short-term, the 
						signs of a recovery are very slow in the regional 
						aviation sector. While in the long-term, much depends on 
						the question of an open air space, access to airports 
						and free competition.”<br>
						<br>
						Home to 65 airlines with 1,029 aircraft in service, 
						operating from 70 commercial airports, the Middle East 
						has 13 air navigation service providers. The combined 
						value of the Arab aircraft fleet is expected to rise to 
						nearly $200bn in the next 15 years. Fuelled by strong 
						growth in regional traffic, IATA has increased its 2012 
						profit forecast for Middle East airlines from $300mn to 
						$500mn. Moreover, Arab League plans to spend nearly 
						$200bn on the purchase of new aircraft in the next 15 
						years.&#8194;<br>
