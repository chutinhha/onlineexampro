				<font face="Verdana" size="4" color="#C00000">Setting the 
				National Agenda</font></b><p align="justify" class="style13">
				The fourth edition of Oman Debate held on December 3, 2012 
				proved to be a path-breaking event for its candid deliberations, 
				open discourse and commitments made by senior government 
				officials during the Debate. There was an all round acceptance 
				on the need for more transparency, better follow-up and diligent 
				implementation of policies by the government. The private sector 
				espoused the need for a skilled Omani workforce, better 
				industrial relations and less of red tape. The Debate brought 
				out the Sultanate’s success and the areas that it still needed 
				to work upon. Improving the standards of education, fostering 
				better work ethic, diversifying the economy’s base and 
				geographically dispersed development were the key challenges 
				enumerated by the panelists and audience.<br>
				<br>
				OER is working on an assiduous follow-up mechanism to ensure 
				that the key issues raised are taken to their logical 
				conclusion. We are in the process of drawing up a detailed list 
				of recommendations that were made at the Debate. This list will 
				be presented to HE Ali bin Masoud bin Ali al Sunaidi, the 
				Minister of Commerce and Industry. We are confident that these 
				will help in addressing the various concern areas of the 
				corporate sector, strengthening the policy making and 
				implementation process with an eye on citizens’ welfare.<br>
				<br>
				On behalf of the entire team of OER, I would like to wish all 
