						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Apple is a compelling buy<br>
						</font><font face="Verdana" size="2">
                		<br>
						Apple will rebound once tax related selling ends in 
						January and the new taxes on capital gains are known to 
						the stock market<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						Apple’s fall from grace was as swift as it was 
						spectacular. Apple peaked at 705 in September and fell 
						as low as 507 in mid-December. The reasons for Apple’s 
						200 point fall are not convincing. A fabled analyst at 
						UBS cut its 2013 earnings per share estimate from 51 to 
						47 expecting slower sales of the iPhone 5 in China. Yet 
						UBS still has a price target of $700 on Apple, down from 
						$780. This is almost 40 per cent higher the stock’s 
						current trading price on Wall Street. Apple’s failure to 
						give its shareholders a special dividend (given its 
						legendary cash hoard) is cited as a reason for selling. 
						I disagree.<br>
						<br>
						Apple is a growth company and special dividends are not 
						the momentum ballast for growth companies. Apple’s 
						shareholders can expect one of the most epic share 
						buyback programmes in the history of Wall Street. Tax 
						uncertainty selling is, of course, a factor in Apple 
						since its shares have proven so profitable for investors 
						since it bottomed in early 2009. Yet this argument only 
						reinforces my conviction that Apple will rebound once 
						tax related selling ends in January and the new taxes on 
						capital gains are known to the stock market.<br>
						I concede that Apple has broken significant chart 
						related technical milestones, including its 200 day 
						moving average and the proverbial death cross. To 
						technical analysts on Wall Street, Apple is broken 
						share. However, a broken stock is not a broken company. 
						Apple dominates the global markets for tablets and 
						smartphone with its unique, innovative products. Apple 
						is one of the most undervalued companies in Silicon 
						Valley relative to its growth potential, now that it has 
						fallen 26 per cent since its September high.<br>
						<br>
						<b>Signs of Resilience</b> <br>
						Apple’s China opportunity is still in its embryonic 
						stage and its products are considered status 
						symbols/cutting edge in the world’s biggest mobile phone 
						market. China’s mobile market is simply colossal, with 
						1.09 billion mobile subscribers and market growth rates 
						are as high as 15 per cent. Smartphone penetration rates 
						are a mere 19 per cent in China, a huge opportunity for 
						Apple (and Samsung). Apple’s deal with China Mobile (the 
						biggest carrier in the PRC with 700 subscribers) could 
						be a catalyst for it. Even as blowout revenues and 
						earnings in January vindicate its unique 
						software/services/hardware ecosystem business model, I 
						believe Apple will rise to 600-620 in the next three 
						months. Why?<br>
						<br>
						One, the product launch momentum in Apple will only 
						accelerate as the iPad Mini/4th gen iPad extend Apple’s 
						tablet market penetration on a global scale, as 
						mobile/Wifi versions of the two devices only add to the 
						sizzle. Two, China Mobile will begin selling the iPhone 
						in 2013 even as the iPad Mini is the ultimate PC killer 
						in the Middle Kingdom and emerging markets. China Mobile 
						alone has the potential to sell 80 million smartphones 
						in China.<br>
						<br>
						Three, valuations matter and Apple enterprise 
						value/EBITDA is now a mere six, the cheapest in a 
						decade. This is value that, irrespective of capital 
						markets temper tantrums, makes Apple irresistible to me. 
						After all, Apple traded at an enterprise value/EBITDA of 
						eight last September (the 700 high) and 12 times back in 
						2010 on the eve of its spectacular new product growth 
						cycle.<br>
						<br>
						Four, the supply chain/shipment delay/component risk of 
						Apple are overblown. There is no reason to expect Apple 
						will not sell 46 – 48 million iPhone 5 units in 4Q 2012, 
						as no global recession is in sight.<br>
						<br>
						Five, the glory days for Apple were 2010 – 11, Apple is 
						still the most innovative consumer tech company on 
						planet with a global addressable market with at least a 
						20 per cent secular growth rate.<br>
						<br>
						Six, in a world of bankrupt countries and overleveraged 
						state/crony capitalist corporates, Apple has a stellar 
						balance sheet, $125bn in cash (or $128/share) and no 
						long term debt, with the potential to generate $10bn in 
						cash every quarter in 2013. This makes Apple an ideal 
						candidate for long term put LEAP sales to generate 
						premium income, in my opinion. The mathematics of 
						extrapolation can, however, be misleading. After all, an 
						Oxbridge statisticians who examined trends in the 
						biological horse residual carriage traffic in Piccadilly 
						Circus in 1900, the year Queen Victoria died, 
						extrapolated that London would be covered with 70 feet 
						of horse manure by the year 1980? Yet I still 
						extrapolate Apple cash flow at $250 a share in 2015.<br>
						<br>
						Seven, Apple will not announce a special dividend, 
						though the payout ratio may well rise as the free cash 
						flow tsunami builds. I believe the Apple will accelerate 
						its share buyback program, Apple could well buy back 
						$50bn in the next three years. This will be hugely 
						bullish for the shares.<br>
						<br>
						Eight, the short interest/put option on Apple convince 
						me a squeeze is all too possible if Apple beats its 
						whisker number next month. This could mean a $50 – 80 
						post earnings spike in Apple in January.<br>
