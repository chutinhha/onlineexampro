						<font face="Verdana" size="1">
						Shangri-la’s Barr Al Jissah Resort and Spa introduced at 
						its laundry a breakthrough dual power washing technology 
						CLAX High Efficiency Laundry Programme (HELP) based on 
						new bleaching system and improved dosage precision. It 
						is designed specifically to perform in low temperature 
						and low pH applications.<br>
						<br>
						In partnership with Diversey’s High Efficiency Laundry 
						Program (HELP) chemical washing system, most of 
						Shangri-la’s hotels now carry lower temperature laundry 
						facilities. A sustainable match between washing machine 
						type, fabric, water consumption and temperature, this 
						green initiative encourages the use of less chemicals 
						and washing cycles thereby extending the linen’s 
						lifespan and increasing machine and labour productivity. 
						The whole wash cycle using the high temperature chemical 
						takes about 12 steps while that with CLAX HELP will take 
						only nine steps.<br>
						<br>
						Since the implementation of the low temp chemical in 
						June 2010, the hotel was able to save 31.27 per cent on 
						its chemical consumption (YTD); 6.13 per cent YTD 
						savings on electricity consumption; 44.89 per cent YTD 
						savings on water consumption; and 34.07 per cent YTD 
						savings on gas consumption. Furthermore, it regularly 
						monitors and keeps track of its electricity, water, and 
						gas consumption trends as these data are also used for 
						the review and inspection of United Registrar Systems 
						(URS) as part of its commitment to ISO 14001.<br>
						<br>
						“Protecting the environment, primarily where our 
						business operates in, is among the five focus areas of 
						our Corporate Social Responsibility. Therefore our goal 
						is to progressively reduce our environmental footprint 
						by consuming least possible amount of energy and water 
						in our Laundry Operations,” says Sreedahr Suresh, 
						director of housekeeping at Shangri-la’s Barr Al Jissah 
						Resort and Spa. “Our company has made its commitment to 
						social responsibility that originates from our core 
						values. We make every effort to be able to sustain our 
						environmental footprint by finding ways and adopting 
						only the best solution whereby both our business and 
						community/society can benefit from. To date, 
						Shangri-La’s Barr Al Jissah Resort and Spa is the only 
						Resort Hotel in Oman that uses Diversey’s CLAX High 
						Efficiency Laundry Programme in the laundry operations.”<br>
