				<img border="0" src="images/autotalk-jan-13-1.jpg" width="400" height="306" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				With an impressive lashing of technology and safety features 
				backed by the legendary quattro drive, you need to think twice 
				before you get into an Audi Q7 because you may never want to get 
				out of it, writes Visvas Paul D Karra after an extensive test 
				drive in the mountains and motorways of the Sultanate<br>
				<br>
				If looks can be deceptive, then the Audi Q7 fits the bill. With 
				its smooth lines and long length, you could be thinking that 
				this is one fine car for urban drive. But it is only when you 
				get behind the wheel and experience the drive that you really 
				get a feel of this awesome vehicle as it offers an ideal 
				combination of a classic urban beauty and a gentle offroad beast 
				as well. <br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-jan-13-2.jpg" width="300" height="240" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
				The design of the Audi Q7 sets new standards as it creates a 
				high-quality overall appearance by means of a synthesis of 
				robustness and sporty, elegant vehicle lines. The dynamic roof 
				line and high shoulder section borrow the sporty form of a coupé 
				while the powerful front with distinctive, chrome-framed Audi 
				Singleframe, and vertical chrome struts arouses self-confidence 
				and radiates a sense of presence. With the strong, V-shaped 
				pointed bonnet, the bumpers with standard contrasting paint and 
				the drawn-back headlights with daytime running lights, the Audi 
				Q7 is always striving to push forward, even when at a 
				standstill. The concise sills and high ground clearance 
				emphasise its strong drive side.<br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-jan-13-3.jpg" width="300" height="188" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
				<b>Quottro-safe</b><br>
				To take you on further, the Audi Q7 skillfully uses all of its 
				power. Its innovative quattro drive concept – a permanent 
				all-wheel drive – distributes the drive forces variably to the 
				front and rear axle just as it is required for that particular 
				driving situation. This enables superb traction, good grip and 
				impressive propulsion, both on and off road. The Electronic 
				Stabilisation Programme ESP supports quattro in the Audi Q7 and 
				was especially extended to include a selectable offroad mode for 
				the Audi Q7. With this, the ESP ensures even more traction and 
				braking power when off-road.<br>
				<br>
				The Q7 has minimal car body movements like rolling or pitching 
				motions even when cornering at high speeds or braking thus 
				ensuring a comfortable driving feeling and better grip. The 
				adaptive air suspension combines the sporty driving character of 
				a coupé with the excellent suspension comfort of a Saloon, by 
				enabling damping and vehicle height to be variably regulated.<br>
&nbsp;</font><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-jan-13-4.jpg" width="300" height="200" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<br>
				We got the Audi Q7 3-litre TFSI 272 bhp V6 variant for our test 
				drive and boy what a ride it was. Thanks to the motor 
				racing-tested TFSI technology, the Audi Q7 deploys its 
				tremendous power and dynamism over practically the entire speed 
				range consisting of 8 automatic gears (tiptronic). Consuming 
				hundreds of kilometers within no time is as smooth as a hot 
				knife in a butter but could drain a hole in the pocket as you 
				burn rubber on the smooth highways that Oman is famous for.<br>
				<br>
				The interiors are as luxurious as those of a sedan and hardly 
				any outside noise is heard inside the cabin allowing you to soak 
				in some good music on the Bang &amp; Olufsen Advanced Sound System. 
				More than 1,000 watts and 14 separate loudspeakers provide an 
				outstanding sound experience. We couldn’t find any USB drive 
				socket even though it had two slots for SD cards. You also need 
				to buy a separate kit for Ipod docking.<br>
				<br>
				All in all, the Q7 will give the rivals in its class a run for 
				their money while having utter disregard for the feelings of 
				others on the road.</font></td>
