						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">OER CEO 
					Golf on January 24 at Al Mouj</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Get ready 
					for an exciting day on January 24 at the Greg 
					Norman-designed Al Mouj golf course at The Wave, with Texas 
					Scramble for golfers; golf academy for golfing initiates; 
					and an evening of high voltage entertainment</font></b><p align="justify" style="margin: 3px 10px"> 
					&nbsp;<p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo-13.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>The 
				9th Edition of OER CEO Golf 2013 will tee-off at the spectacular 
				Al Mouj golf course at The Wave on January 24, 2013. There 
				couldn’t be a better way to kick-start your New Year than being 
				found at Oman’s premier business calendar event, the OER CEO 
				Golf.<br>
				<br>
				For OER CEO Golf, Al Mouj will transform itself into a natural 
				meeting ground for business and golf as an elite gathering of 
				business leaders and corporate heads come together for the 
				Sultanate’s biggest networking event amidst undulating verdant 
				settings.<br>
				<br>
				In this day-long event, golfers will be playing the Texas 
				Scramble competition, reveling in the PGA-standard course’s 
				exciting sea side settings designed by golfing legend Greg 
				Norman, while golfing initiates will get their professional 
				introduction at the Golf Academy, followed by an evening of high 
				voltage entertainment in the evening.<br>
				Nawras Business is main presenter of OER CEO Golf 2013 while 
				Cadillac is the Associate Partner. Rolex from Khimji’s Watches 
				is the official time keeper and Samsung the prize partner. 
				Muriya Tourism Development and Mouawad are the support partners 
				while Times of Oman and Al Shabiba are the publicity partners. 
				Other category partners include Hi and Al Youm Al Sabe (Media 
				sponsors); Talentz (AV, Light &amp; Sound); Axa Insurance 
				(insurance); Mazoon Printing Press (print); Aggreko (energy); 
				and Infoline (Call Centre).<br>
				<br>
				Since its conception in 2005, OER CEO Golf has been a pioneering 
				initiative from OER and United Media Services, its publisher. 
				The level of participation seen in OER CEO Golf only goes on to 
				show the importance of this event in Oman’s corporate calendar.<br>
				<br>
				OER CEO Golf 2013 offers revitalisation for Oman’s corporate 
				heavy weights, giving them an opportunity to network and compete 
				on the greens. There are few events of this nature that can 
				boast of such variety and all-round entertainment.<br>
				<br>
				<b>Exciting day<br>
				</b>Designed by Greg Norman, the course at Almouj Golf provides 
				challenging 18 holes of varying lengths and configurations and 
				will set your pulse racing as you swing your club against the 
				backdrop of the sea and the sound of the surf brought to you by 
				the cool January wind. Norman has captured the essence of the 
				surrounding environment and enhanced it to produce a links style 
				course that runs alongside over two kilometres of beach. The 
				course is 7,342 yards in length, and highlights include a 
				daunting Par 3 island green and a challenging Par 5 that 
				stretches for over 600 yards. The breathtaking views of the Sea 
				of Oman and the Hajar Mountains are visible from everywhere on 
				the course.<br>
				<br>
				The evening’s programme include a gala dinner, world-class 
				entertainment, socialising, exciting prizes, and more.<br>
				<br>
				Meanwhile, Simon Duffield, general manager of Almouj Golf at The 
				Wave, says that it is an honour to partner OER CEO Golf 2013. 
				“Almouj Golf, which represents quality, pioneering business 
				approaches and world class standards &amp; facilities as a brand 
				aligns perfectly to OER and also the top business people in 
				Oman. So I’m sure many corporate ‘tie-ups’ will happen resulting 
				in some exciting future partnerships and deals,” adds Duffield 
				while speaking the partnership between OER and Al Mouj for OER 
				CEO Golf.<br>
				<br>
				<b>Promoting golf<br>
				</b>According to Duffield, being one of the biggest corporate 
				events of the year and OER’s dedication to promoting golf in 
				Oman coupled with Almouj Golf as the host venue all add up to 
				the perfect match and this event is an absolute must on any 
				golfing and/or business calendar.<br>
				<br>
				“The businessmen and business ladies are one segment of the 
				community who know the importance of these events as well as 
				being a golf club member. As someone once said, some of the 
				biggest corporate deals in history were fashioned on the golf 
				course!”, maintains Duffield.<br>
				<br>
				In another development, Chandra Lahiri, CEO, Muriya Tourism 
				Development, says that OER CEO Golf is a genuinely pioneering 
				event in Oman and his company is pleased to be coming on board 
				as a partner.<br>
				<br>
				Describing the reasons for Muriya’s partnership with OER CEO 
				Golf 2013, Lahiri adds, “Muriya will be building three 
				world-class golf courses in Oman over the next few years, which 
				will actually double the country’s golf courses from the current 
				three, pioneered by the Muscat Hills Course. We expect to be a 
				very significant player on the Oman golf scene – hence our 
				partnership with OER CEO Golf is but natural.”<br>
				<br>
				Continuing further, Lahiri says, “What is particularly admirable 
				in OER’s efforts here is that they are among the very few who 
				saw the potential for golf in Oman, when it was still in its 
				infancy in the country. Bravo, OER!”.</td>
