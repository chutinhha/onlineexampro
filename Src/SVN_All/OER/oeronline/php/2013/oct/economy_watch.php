<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">
Investors appear to have given up on emerging markets. What was a de riguer, must-have investment just a few years ago, has now turned into an area of concern. The list of issues is long – financial vulnerability, over-investment, credit booms, heavy state involvement, external deficits, falling margins, and the potential reversal of US quantitative easing. Gone are the bullish long-term stories of urbanisation, demographics, rising GDP shares of the world economy, the middle class consumption boom, the inevitable 'tryst with destiny' of these laggard economies. So, what is an investor to do with this conflicting information?
<br />
<br />
Merrill Lynch wants investors to be tactically bullish – the same as a month ago. There are clearly longer-term challenges and risks – much depends on the response of policy makers – both in developed and emerging markets. Much also depends on how the corporate sector in emerging markets confronts and adapts to this uncertain environment. Merrill Lynch's approach is to cross this river by feeling the stones, one at a time, as Deng Xiaoping recommended. The report feels that valuations in the key, large emerging markets are compelling, sentiment is quite sour, China's (and the world's) economic data are surprising positively, and the terms of trade for Asia are improving sharply. The report is overweight on Korea, Russia, China and India (a contrarian view). It is underweight on Mexico, Malaysia, Thailand, and Chile. For the rest it is neutral.
