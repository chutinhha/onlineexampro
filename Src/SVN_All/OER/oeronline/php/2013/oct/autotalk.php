				<br>
               <font face="Verdana" size="1"><b>Enhanced dimensions</b><br><br>
                The i10 is longer (+80 mm), wider (+65 mm) and lower (-50 mm) than its predecessor. It provides best-in-class legroom and cargo capacity. Front and rear passengers benefit from combined legroom of 1.890 mm, while trunk capacity has been boosted by more than 10 per cent to 252 litres with all seats in place.<br>
                <br>
                The new gen i10 is based on an all-new platform featuring a five millimetre longer wheelbase and relocated rear dampers that deliver more precise wheel control over all types of road surface. To take full advantage of its enhanced dimensions, New Generation i10 has also been carefully tuned to deliver refined driving dynamics and low noise levels, for an experience close to the standard of B-segment cars.</font><br><br>
