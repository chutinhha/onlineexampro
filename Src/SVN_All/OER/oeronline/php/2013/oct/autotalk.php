                        <b> Hyundai Motor introduces new levels of true value with its spacious and sophisticated New Generation i10 for Europe. Mayank Singh reports from the Internationale Automobil-Ausstellung in Frankfurt</b>
                        <p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
