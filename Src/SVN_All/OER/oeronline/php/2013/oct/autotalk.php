				<font face="Verdana" size="1">
				Hyundai Motors marked the global debut of the new generation i10 at the Internationale Automobil-Ausstellung (IAA) in Frankfurt on September 10, 2013. The high-profile launch was one of the highlights of this year's IAA.<br>
				<br>
				The new i10 demonstrates significant progress with the car possessing abilities and features typically found in higher segments. Generous levels of equipment, space and functionality, coupled with sophisticated design, refined ride qualities and a quiet cabin, sets new levels of 'true value' for Hyundai.<br>
                <br>
                Buyers of New Generation i10 will be offered the highest levels of comfort and safety equipment, with many features more common to segments above. Available equipment such as automatic climate control, heated leather steering wheel and cruise control with speed limiter provides bigger-car comfort, while safety features include standard-fitment stability control and front curtain airbags.
                <br>
