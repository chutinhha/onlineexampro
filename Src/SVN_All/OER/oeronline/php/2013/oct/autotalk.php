            <br><br>The European Hyundai design team, led by chief designer Thomas Bürkle, has created a dynamic shape using fluidic sculpture, Hyundai Motor Company's design philosophy, to emphasise the new car's athletic proportions and generous external dimensions. The refined exterior is mirrored by a stylish interior that features high-quality materials on key surfaces.<br>
          <br>
          Allan Rushforth, senior vice president and COO of Hyundai Motor Europe, commented, "New Generation i10 is another significant step forward for Hyundai Motor. Every aspect of the car has been designed and rigorously developed to meet the demands and needs of European consumers. The new generation i10 fully embodies Hyundai's 'true value' proposition, bringing higher levels of features and functionality than many buyers would expect to find in a vehicle of this segment."<br> 
          <br>     			
				As with 95 per cent of Hyundai cars sold in Europe, the new i10 has been designed and developed at the Hyundai Motor Europe Technical Centre in Rüsselsheim, Germany, making it the first A-segment Hyundai to also be built in the region.<br>
				<br>
				Set to enter production from September 2013, the i10 will be made at Hyundai's factory in Ízmit, Turkey. The annual capacity of the plant, which already produces the B-segment New i20 model, is rising to 200,000 cars, following a multi-million Euro investment in infrastructure in the region. Once the i10 goes on sale across Europe, 90 per cent of Hyundai cars sold in Europe will be built in the region.<br></font></td>
