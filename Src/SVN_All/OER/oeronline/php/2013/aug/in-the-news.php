					<b><font face="Verdana" size="4" color="#C00000">Unravelling the critical areas for insurance</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The Oman Insurance Association, as part of its efforts to raise the standards of professionalism among the market players, organised a workshop on motor insurance and natural catastrophes.<br /> An OER report</font></b>
						<br />
                        <br />
                        <center><img src="images/in_the_news_1.jpg" border="0" /></center>
						<p align="justify" style="margin: 3px 10px"> 
					<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">The Oman Insurance Association (OIA) is actively involved with all the insurance companies in the country to develop a better understanding and cooperation which could help in highlighting the critical importance of insurance among the public and make the insurance sector more robust. One of the important factors that needs to be disseminated among the people is that a viable and dynamic insurance sector is the foundation of all other industries and underpins the national economic growth.
<br />
<br />
In line with its commitment to ‘Underwrite Oman’s Progress’, OIA organised a workshop on ‘Motor Insurance and Managing Natural Catastrophes’ for the industry to shed light on critical areas of insurance which need to be addressed by the players in the Sultanate’s market.
<br />
<br />
The OIA, which has been established as a trade body consisting of insurance companies, insurance brokers and loss adjusters has 31 companies as members and is working on raising the standard of professionalism and awareness on the role insurance can play in supporting people’s life.
<br />
<br />
Says Llyod East, chairman, external affairs and media committee, OIA, “With unprecedented economic growth in Oman, the need for insurance against catastrophic events, accidents or critical illness has become stronger and more urgent. In this regard, one of the key objectives of the association is to build a coherent insurance industry that underwrites Oman’s growth. This workshop was a big hit, as the experts discussed and debated the insurance scenario in the country and made constructive recommendations.”
<br />
<br />
The workshop had keynote speakers from Hannover Re, one of the leading reinsurance groups in the world and Frost & Sullivan, an American firm. Members of the motor committee, property and casualty committee of the OIA also presented papers at the workshop.
<br />
<br />
According to Lloyd, the workshop was a huge success as it was a meaningful learning experience for everyone present. The international perspectives and proposals presented by eminent overseas presenters were invaluable and will be definitely beneficial in the context of Oman’s future insurance needs and requirements.
<br />
<br />
Meanwhile, the OIA has decided to urge the Capital Market Authority (CMA) to bring in a series of changes in the no claims discount (NCD) scheme in motor insurance, which was implemented in 2012. The main recommendation is to introduce a standard certificate by all insurance companies so that all consumers recognise one form for a no claims discount.
<br />
<br />
The OIA is debating on how best the industry can implement the NCD to ensure that consumers get a really fair and good deal from insurance firms. 
According to reports, the non-life premium is $750mn per annum in Oman and approximately half of the market premium is from motor insurance. Motor insurance segment continues to see good growth rates over the last couple of years in terms of revenue. But the profitability of motor insurance continue to remain challenged for several years in Oman, driven particularly by the high level of accident rates and relatively competitive levels of premiums. 

