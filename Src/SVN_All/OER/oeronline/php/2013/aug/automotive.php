				<font face="Verdana" size="2">
				<b>With its sophisticated construction and reliable CA100 alloy, the Energizer Premium battery is the best choice for modern vehicles with lots of simultaneously operating electronic devices</b></font><p align="center" style="margin: 10px; ">
				<img border="0" src="images/automotive_1.jpg" width="396" height="304"><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				Most of us have heard and seen the name Energizer. They are synonymous with batteries for your home electronics, but what is less well known is that although new to Oman, Energizer, produced today by Johnson Controls, has been in the automotive batteries business since the 1980s and is one of the most popular and trusted vehicle batteries the world over. As with all Energizer batteries, performance and quality is of the essence, and these batteries are no different. As such, they maintain the exceptionally high standard that Energizer is famous for.
Finding the best car battery to suit your vehicle and needs can be quite a challenging task. There are many types of battery all claiming to be the best in their field with performance. Many people make their decisions based on prices, and in the current economic climate that would appear to be an attractive solution to save money. However, the key lies in finding a battery that provides the perfect balance of price, performance, reliability and quality. Energizer is one such brand.

				<br />
				<br />
			In automobiles, Energizer batteries provide reliable starting power thanks to their streamlined range of batteries for practically every application – even vintage cars. With its sophisticated construction and reliable CA100 alloy, the Energizer Premium battery is the very best choice for modern vehicles with lots of simultaneously operating electronic devices. It also has a considerably longer service life and is well equipped to handle 14.8-volt electrical systems. 
Their exceptional performance and reliability comes courtesy of the ‘True Strength’ provided by PowerFrame stamped grid technology. This innovative and robust grid structure reliably prevents premature battery failure caused by corrosion, ensuring efficient electrical flow for consistently high power over the life of the battery. 

				<br />
				<br />
				In contrast to conventional grids, PowerFrame grids are not cast or laid out, but precision-punched. This unique technology sets new standards and strengthens the material, whereas the material used for conventional grids is already weakened during the manufacturing process. 
				<br />
				<br />
				However, for vehicles featuring advanced start-stop technology with regenerative braking and additional fuel-saving technologies, regular batteries just won’t do. To cope with the additional load and provide better cyclic stability, Energizer has developed two very specific batteries – the Energizer Premium AGM (Absorbent-Glass-Matt) and Energizer Premium EFB (Enhanced-Flooded-Batteries).
				<br />
				<br />
			The unique construction of an AGM battery, which stores the acid in a glass mat and can therefore make considerably better use of the cell volumes, can easily manage heavy use exceeding 4,500 watts. This applies in particular to vehicles in the upper mid-range and luxury segment, which tend to feature numerous electronic convenience and safety devices. Taxis also require more energy because of the high cycle requirements of stop-and-go traffic, and a Premium AGM battery effortlessly provides it.
				<br />
				<br />
				On the other hand, Energizer Premium EFB batteries are ideal for use in vehicles with standard entry level start-stop functionality, ensuring that electrical devices continue to be reliably supplied with power while the engine is switched off and that the engine can be restarted in seconds when it’s time to pull away. They not only boast cyclic stability that’s twice that of conventional batteries, but also offer sustained performance levels even when discharged.
				<br />
				<br />
				In Oman, Sayarti is the official dealer of Energizer and provides the entire range of Automotive and Commercial batteries. This includes the Energizer, Energizer Plus and Energizer Premium batteries as well as the AGM and EFB for passenger car vehicles with start-stop technology. The batteries are available at all automotive retail shops and also at Sayarti outlets in Wadi Kabir, Athaiba, Qurm City Centre and Muscat Grand Mall. 
