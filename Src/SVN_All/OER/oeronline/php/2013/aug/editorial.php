				<font face="Verdana" size="1">
				Oman’s telecom sector is a story of two parts. Let’s start with the good news -- the Sultanate was ranked 32 out of 144 countries by the World Economic Forum’s 2013 Global Competitiveness Report. The report stated: “Oman is progressing towards a knowledge and innovation-driven economy using a comprehensive and competitive communications industry, which is one of the key drivers for economic growth.”
 
				<br />
				<br />And this is not a one-off case. An AREGNET (Arab Regulators Network) report lavishes praise on the broadband service in Oman describing it as being amongst the most competitive in the region. The 2012 telecom retail price benchmarking report for Arab countries affirms that mobile broadband in the Sultanate is the second cheapest out of 22 countries in the region. 

				<br />
				<br />
The flip side of the coin is that all these developments have come at a steep cost with telecom operators struggling to stay competitive. The sector faces numerous challenges like a decline in revenue from the traditional services like landlines, voice and SMS; intense competition with a multiplicity of players; higher cost of human resources and ever changing technologies. 
<br />
<br />

According to a 2013 report by Business Monitor International, the Sultanate’s telecom market is limited by the small population and is fast approaching saturation in services like voice, forcing operators to sell high-value products like broadband to sustain revenue growth. Visvas Paul D Karra gives an in-depth take on the industry in this issue’s cover story. 
