                <span class="style27"><strong>The flagship sedan</strong></span>
		        <br />
		       						
<p align="justify" style="margin: 3px 10px; line-height:17px">
					
<font face="Verdana" size="1">
Tom Lee, Vice President, Head of Middle East Regional Headquarters, Hyundai Motor Company talks about the new Centennial and Hyundai’s differentiation in an interview with OER
<br />
<br /><center><img src="images/face.jpg" border="0"></center><p align="justify" style="margin: 3px 10px; line-height:17px">

<b>The all new 2014 Centennial has been launched recently. What can customers look forward to in the car? 
</b><br />
The New 2014 Centennial has recently gone on sale across the Middle East. Hyundai’s flagship luxury sedan has become better set to compete against other luxury car manufacturers, with a wealth of additional features and technologies to both the exterior and interior.  
<br /><br />
Alongside a number of subtle design upgrades to the exterior, the dashboard has been totally redesigned and incorporates a new premium clock created in collaboration with luxury lifestyle design firm, Innes & Lister Inc., while the new centre console sports a cleaner feel thanks to the introduction of a new shift by wire gear lever. The cabin exudes quality, with high class leather and wood trim all around. 
<br /><br />
The New 2014 Centennial also features a wide range of the latest technologies. For improved safety, it offers a head-up display, which projects speed and navigation information on the windscreen, allowing drivers to keep their eyes on the road, while a Blind Spot Detection system alerts the driver of any vehicles or objects that are in the car’s blind spots.
<br /><br />
The New 2014 Centennial also features a wide range of the latest technologies. For improved safety, it offers a head-up display, which projects speed and navigation information on the windscreen, allowing drivers to keep their eyes on the road, while a Blind Spot Detection system alerts the driver of any vehicles or objects that are in the car’s blind spots.
<br /><br />
Under the bonnet the New 2014 Centennial is equipped with Hyundai’s acclaimed GDi engines for the very first time. The top-of-the-range powerplant is the award-winning TAU V8 5.0-litre GDi unit, which delivers maximum power of 430ps, a top speed of 240kph and 0 to 100kph in 5.8 seconds. A 334ps Lambda V6 3.8-litre GDi engine is also available and both units are mated to an eight-speed automatic transmission.

<br /><br /><b>
What are the most important changes made in the New 2014 Centennial? 
</b>
<br />
As previously mentioned all new additions and enhancements to the New 2014 Centennial are important, as they have collectively enhanced the car in terms of technology, comfort and performance. 
<br />
<br />
<b>
With all luxury cars, comfort is one of the most important aspects of the car. Tell us what comfort elements does the New 2014 Centennial have? 
</b>
<br />
As befitting a luxury car of this nature, the New 2014 Centennial provides the highest standards of comfort and convenience. The rear seats have been completely redesigned for greater comfort, while the front seats have also been adapted. Passengers in the back can now enjoy new dual 9.2inch LCD rear monitor entertainment screens and control their travel experience via a new DIS system.
Ensuring that Hyundai’s large luxury sedan offers the highest standards of ride comfort, the electronically-controlled multi-link suspension has been fine-tuned, while an air suspension option is also available. At the same time, improved tyre wheel rigidity, newly designed wing mirrors for less wind noise, quieter engines and improved transmission mounting bracket rigidity help to make every journey as quiet as it is smooth.

<br />
<br />
<b>
Have you contemplated a premium spin-off brand, as Toyota and Nissan have done with Lexus and Infiniti respectively?
</b>
<br />
There are no plans to introduce a separate brand for our premium line up. By investing in our luxury car range, we are looking to further raise the perception across the whole brand. In addition to producing these quality cars, we have introduced Premium Lounges, that are in all Hyundai showrooms ensuring  that customers enjoy premium levels of treatment, as well as the Premium Assurance Plus Programme for our luxury line up, all with the objective of lifting the Hyundai badge image and ensuring a first class experience for our customers.
<br />
<br />
<b>
What is the assurance policy that Hyundai gives for its Premium cars? 
</b>
<br />
From January 2013 our customers from across the Middle East now can enjoy higher levels of customer satisfaction with Hyundai’s Premium Assurance Plus Programme. This is available with all Centennial and Genesis models as standard, giving customers added peace of mind and giving them further benefits that exceed what found in other assurance programmes. 
<br /><br />
This is in addition to a guaranteed buy back value, if they decide to sell it back to the distributor. Another key feature of the new programme is the free maintenance for five years or 100,000km, a significant step-up from the three years/60,000 km offered previously. All models also come with a five-year/unlimited kilometre manufacture provided warranty. 
<br />
<br />

<b>
What are the most important benefits of the programme?
</b>
<br />
A key feature of the new programme is a highly competitive, guaranteed buy-back value, valid if the customer opts to sell their car back to their Hyundai distributor. <br />
They also get free maintenance for five years or 100,000km, a significant step-up from the three years/60,000 km offered previously. All models also come with a five-year/unlimited kilometre manufacturer provided warranty, five years, 24/7 roadside assistance in the event of a vehicle breakdown, a five years’ service courtesy car loan while their vehicle is being repaired, as well as their very own life-time dedicated service advisor.

<br />
<br />
<b>
What are Hyundai’s strategic plans in the Middle East in the coming years?
</b>
<br />
Globally and in the Middle East we want to be the most beloved brand, with our new brand slogan “New Thinking New possibilities” reflecting our desire to create products that benefit the consumer, and encouraging people to develop new thinking. We don’t want to just be number one in terms of sales, but we also want to be recognised as a brand that works on creating a better and sustainable future. 
