<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">
The Omani economy continued to grow in 2012, supported by high oil prices and increased production, and by fiscal expansion. Overall real GDP growth is projected to have reached 5.0 per cent in 2012, up from 4.5 per cent in 2011. Due to new recovery technologies and investment prompted by high oil prices, oil production is estimated to have increased in 2012 and 2013. The non-oil sector is projected to grow at 5.8 per cent in 2012, supported by public investment, and increased activity in services sector. The overall consumer price inflation declined to 2.9 per cent in 2012 from 4 per cent in 2011, given moderating international food prices and government subsidies on core goods and services. The fiscal balance is estimated to remain in surplus in 2012, as high hydrocarbon revenue has offset the cost of the recent fiscal expansion. Revenue from exports of crude oil and liquefied natural gas (LNG) in 2012 is expected to bring the current account surplus to 11.6 percent of GDP.
<br />
<br />
The banking system is sound, profitable and well-regulated. The banking system-wide capital adequacy ratio was 16.0 per cent and nonperforming loans remained low at 2.1 per cent of total loans in December 2012. Monetary policy remains accommodative. Total credit continued to grow by 14 per cent in 2012.
<br />
<br />
While the near- and medium-term growth outlook remains positive with robust non-oil growth and moderate inflation, fiscal and external balances are projected to turn to deficit. Recent spending initiatives, including the government job creation in 2011–13, have increased expenditures by 70 per cent between 2010 and 2012, and reduced the policy space to respond to shocks. A sustained fall in oil prices could exhaust available buffers and necessitate borrowing to maintain the projected capital spending.
<br />
<br />
<strong>Executive Board Assessment</strong>
<br />
Executive directors of IMF noted that Oman’s strong economic performance has been supported by favourable developments in the oil market, and that the near-term outlook continues to be positive. Directors observed, however, that the recent increase in current spending poses a challenge to medium-term fiscal sustainability. They encouraged the build-up of fiscal buffers and supported continued efforts to promote non-oil growth and diversify the economy.	
<br />
<br />
IMF considered that containing current expenditure, particularly the wage bill, should remain the key focus of fiscal consolidation. They welcomed efforts to increase non-hydrocarbon revenues, and encouraged working with the other Gulf Cooperation Council countries to expedite the introduction of the value added tax. Phasing out fuel subsidies, while ensuring targeted support to protect the poor, will help create fiscal space for infrastructure and social investments. Should downside risks to global conditions materialise, additional consolidation efforts would be required. Directors also encouraged further progress on fiscal institutional reforms, including the introduction of a medium-term budget framework and the establishment of a macro-fiscal unit, to ensure that the budget is consistent with macroeconomic developments.
<br />
<br />
Directors concurred that the exchange rate peg remains an appropriate anchor for monetary policy. They encouraged the central bank to manage liquidity more actively by developing a formal liquidity management framework. The IMF welcomed the authorities’ efforts to establish a regular domestic debt issuance programme, including sukuk.
<br />
<br />
The IMF directors commended the authorities’ prudent financial sector policies, and noted that banks remain well capitalised, with low nonperforming loans. They welcomed plans to implement regulations aligned with Basel III within the internationally agreed timeline. To address emerging risks, Directors saw merit in further strengthening risk–based supervision. They welcomed steps taken to establish a formal coordination committee for macroprudential policies and to strengthen the Anti-Money Laundering/Combating the Financing of Terrorism (AML/CFT) framework.
<br />
<br />
They agreed that diversification into areas with greater employment potential should be a priority, and welcomed the government’s Vision 2020 plan and their focus on developing small and medium-sized enterprises (SMEs). To increase Omani employment in the private sector, they encouraged reducing the wage and benefits differential between the public and private sectors by gradually curtailing high public sector wages, reducing government job creation, as well enhancing education and vocational training. Continued efforts to improve the overall business climate, particularly for SMEs, will also be needed.
<br /><br />
The directors noted that there is scope for improvements in statistics, particularly in the areas of labour market, budget classification, and real estate.  
