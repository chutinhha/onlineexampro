<font face="Verdana" style="font-size: 8pt">
<strong>Can you give us a brief on Hempel’s Oman operation? 
</strong><br />
The Hempel Group is a leader in the production and sales of high performance coatings within the marine, protective, container, yacht and decorative market segments. Headquartered in Denmark, Hempel has 21 factories, 47 sales offices, three main and five regional research and development centres, and more than 150 stock points strategically located around the world. Hempel commenced its journey in Oman more than two decades ago through an agency agreement with Samco Trading & Contracting Co. With its motto of, ‘number one in quality and service’, Hempel Paints has successfully serviced the protective and marine segments, setting new industry benchmarks within Oman
<br />
<br />
<strong>Where does Hempel position itself in Oman’s coating market?
</strong><br />
The Oman coating industry has been growing constantly over the last decade. The sector’s growth has been consistent with Oman’s GDP growth rate and in some years even higher. The annual coating market size is estimated to be in excess of 40 million litres of which decorative paints account for 80 per cent share while industrial paints constitute the remaining 20 per cent. In the medium term, we expect the industry to grow by five to six per cent YOY.
<br />
The protective coating sector has been for some time the key area of commercial investment for Hempel at Oman. A major of our revenue in Oman is generated from our protective coating business, wherein we have attained market leadership position. The decorative segment represents a significant part of the total coating volume requirements in Oman, and is also expected to keep growing. In line with our global strategy ‘One Hempel. One Ambition’, our top priority is to get significantly better market share within this segment. Marine market segment in Oman again is relatively much smaller in comparison to the decorative and protective market size. We have a leading position within this segment.

<br />
<br />
<strong>Can you elaborate on your strategy to penetrate the decorative coating sector where you need to compete with more firmly entrenched players? 
</strong><br />
Hempel will continue to leverage its high-quality product range, with advanced technical support at all levels to retain and further expand its market share. We are driving a significant expansion of our retail network in the Sultanate to serve an ever increasing decorative segment as the market emphasis is shifting towards more sophisticated products and finishes. We are well aware of the need to build relationships among suppliers, architects, clients, consultants and applicators. Thus the whole chain contributes to the success of our products.
<br />
<br />
<strong>Sustainability is the new buzz word within the paint industry. What does Hempel do on this front? 
</strong>
<br />
Hempel is committed to the cause of sustainable development and is playing their part in contributing towards a culture for green buildings.Hempel offers sustainable paint for every section of a building ranging from exteriors to interiors. For example, for exteriors we have Contex Thermogaurd which is a heat insulating paint, and Hempel offers Topaz zero for interiors which has zero level content of VOC to maintain a good indoor air quality. Hempel offers anti-fungal and anti-bacterial paint like Hempel Mouldex for buildings like hospitals and schools which require an extra hygienic environment. Apart from this all of the Hempel decorative paints are 100 per cent free from lead, APEOs and formaldehydes. 
