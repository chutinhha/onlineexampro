					<b><font face="Verdana" size="4" color="#C00000">Digital signages have just signed in</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Digital signages are slowly overtaking traditional signboards which use the print medium, and are here to stay, says Alain Bodenstedt, Partner Sales, EMEA, Scala, while on a visit to Muscat. <br />An OER report<br />
						
</font></b><p align="center" style="margin: 3px 10px"> 
					&nbsp;<p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/media.jpg" >
					<p align="justify" class="style13">
<br />
<br />
Digital signage, a form of electronic display that shows television programming, menus, information, advertising and other messages, frequently utilising technologies such as LCD, LED, plasma displays, or projected images to display content, can be found in both public and private places including retail stores, hotels, restaurants, and corporate buildings, amongst other locations. 
<br />
<br />
Digital signage has won wide acceptance because its content can be both frequently and easily updated, saving the printing and construction costs associated with static signage; and also because of the interactive abilities available through the accompanying employment of such real-world interfaces as embedded touch screens, movement detection and image capture devices.

<br />
<br />
Some of the advantages Scala customers enjoy are the powerful features such as built in template, schedule-based content distribution, variety of integration possibilities, and media playlists to keep you in control of your network from wherever you are in the world and from any internet enabled device. 
<br />
<br />
Sultanate too has caught on to this world-wide innovative nouveau trend, with the traditional advertising using printed medium going out of form. In addition, advertising companies and clients find that content on digital signs can be more targeted to consumers at specific times.
<br />
<br />
Anzyma, an Omani company, has partnered with Scala digital signage company to bring world class digital media networking to Oman and has quickly bagged projects from Omantel, Ministry of Health (MOH), Petroleum Development Oman, OIEC etc where they have implemented the Scala digital signage technology. The long-term benefits of digital signage have been recognised by companies and organisations which have interaction with large numbers of people.	
<br />
<br />
For example, the MOH has implemented Oman’s first health education TV channel, ‘Hayah’ developed by Anzyma. The new health education channel is part of the “Healthy living, healthy nation” initiative by MOH that aims to change people’s lives and promote healthy living. Hayah will create health awareness and wellness programmes that entertain, engage and change the lifestyle of the people in Oman. By harnessing the power of digital technology, this new health education channel engages close to 150,000 people every month including patients, visitors and staff working in health centres across the Muscat region. This region covers 23 health centres that will be displaying the Hayah channel, powered by SCALA InfoChannel digital signage software.	
<br />
<br />
While talking about Scala’s partnership with Anzyma in Oman, Alain Bodenstedt, Partner Sales, EMEA, Scala says his company has been driving more than 500,000 digital media signage screens worldwide thanks to the understanding that digital signage solutions can be used effectively to move products, consumers, and sales metrics. Giving information about Scala, Bodenstedt says that the company, founded in 1987, pioneered the digital signage industry and today remains the world’s largest provider of software for creating digital signage networks, driving more screens than all competitors combined
<br />
<br />
The company’s InfoChannel product line is used for dynamic digital media networks in retail, education, entertainment, government and other industries for diverse applications.
