				for a more inclusive growth. <br>
				<br>
				In response to the Royal Directives of His Majesty Sultan Qaboos 
				bin Said, a Small and Medium Enterprises (SMEs) Development 
				Symposium was held at Saih Al Shamekhat in the Wilayat of Bahla 
				under the patronage of Sayyid Khalid bin Hilal bin Saud Al 
				Busaidi, Minister of the Diwan of Royal Court. The three-day 
				symposium was aimed at putting the contents of the Royal speech 
				at the Council of Oman on November 12, 2012 into practice, 
				instilling entrepreneurship culture in Omani youths, linking 
				education to the labour market’s requirements, diagnosing 
				hindrances in the development of the SMEs and proposing 
				solutions to these difficulties. His Majesty also announced the 
				creation of a RO70mn, Al Rafd Fund to help the youth to develop 
				their SME projects. The outlay of this fund will be increased by 
				RO7mn every year. These measures should go a long way in 
				promoting an entrepreneurial culture in the Sultanate. </td>
