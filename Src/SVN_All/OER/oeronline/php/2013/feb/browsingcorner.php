				Coggan’s ‘Paper Promises: Money, Debt and the New World Order’ 
				examines the flawed structure of the global finance system as it 
				exists today, and shows that the world is facing even deeper 
				imbalances than we are yet grappling with. An OER review</b></font><font face="Verdana" size="1"><br>
				<br>
				The world is drowning in debt, in paper promises. Can we stay 
				afloat? In his masterful new book, Philip Coggan shows how the 
				current financial crisis has very deep roots indeed - in the 
				very nature of money itself. Societies from ancient Greece to 
				revolutionary France have experimented with all kinds of money, 
				from seashells to tobacco, before finally settling on the paper 
				version we have come to know so well. What exactly is money? How 
				has its function changed over decades and centuries? What 
				purpose did the gold standard actually serve? Why, for that 
				matter, is gold still worth anything? Who are the world’s real 
				creditors and debtors today? Above all, what promises do we need 
				to believe to stop the whole system falling apart?<br>
				We take for granted so much about the current financial system. 
				Only by going back to the beginning – to first principles and to 
				our history – can we comprehend where all this debt has come 
				from, and it’s very serious consequences now. In doing so, 
				Philip Coggan examines the flawed structure of the global 
				finance system as it exists today, and shows that the world is 
				facing even deeper imbalances than we are yet grappling with. 
				Where will the world go next? How will we get out of the debt 
				crisis?<br>
				<br>
				The answers can only be found in surprising places from 
				Mississippi swamps, through Chinese economic strategy, to the 
				Wizard of Oz. The way that our paper promises are kept or broken 
				will determine our future. Before we can solve the debt crisis, 
				we need to understand what is going on.<br>
				<br>
				The book starts by looking at the nature and origins of money 
				and how it changed from solid objects to entries on computer 
				screen. Then it recounts how empires and kingdoms rose and fell 
				along with their money, see how individual currencies developed 
				along with global trade and how such currencies changed from 
				symbols of national pride to tools of economic policy.<br>
				And it also explains how debt ceased to be a matter of 
				individual shame and became almost a human right. Finally, it 
				looks at the modern debt crisis in the context of that history 
				and argues that the current position is unsustainable. Many 
				debtors have made paper promises that they won’t be able to 
				keep.<br>
				<br>
				Debt is a ‘promise to pay back’, sounds simple. But modern debts 
				are promise to pay back in promises to pay back (as fiat money 
				is also a form of debt). This makes the economic system very 
				complex. Money these days can be created by central banks with a 
				click of a mouse. Creating money may cause inflation, but it may 
				be vital to help the economy function property or to help avoid 
				mass bankruptcies in our society. Money is the store of value as 
				well as the medium of exchange; we need to find a balance 
				between the two functions.<br>
				<br>
				The futures of debtors and creditors of the world are tied 
				together. And debt is not only the fault of the borrowers, but 
				also a fault of the lenders. All this has implications of the 
				global economy. This book addresses these issues flawless.&#8194;</font></p>
