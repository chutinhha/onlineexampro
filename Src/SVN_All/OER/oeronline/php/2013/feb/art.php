						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Giving wings to ideas</font></b><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="2">
				<b>Shahrukh Khan talks about the changing dynamics of 
				film-making, contribution of Indian cinema and his love of 
				acting, as a run up to his show ‘Temptations Reloaded 2013’ in 
				Muscat. The show is scheduled for February 13 and is being 
				organised by Light &amp; Shadow Enterprises. Mayank Singh reports 
				from Mumbai, India</b></font><p align="justify" style="margin: 10px; ">
				<br>
				<font face="Verdana" size="1">
				<b>
				<img border="0" src="images/art01.jpg" width="200" height="338" align="left" hspace="5" vspace="5">There is growing corporatisation of movie making and the 
				emergence of bigger studios in India. Are these changing the 
				business dynamics of the film-industry?<br>
				</b>On the business front it is getting organised. For example, 
				a film producer like me does not have to deal with individual 
				film distributors anymore. Dealing with individual distributors 
				in itself is not a bad thing, but I am supposedly a creative 
				producer, so it becomes a space that I do not belong to in terms 
				of business. Corporate houses have people who work on that 
				aspect, so we now work with corporate houses and tell them to go 
				ahead and work in tandem with our teams with the distributors 
				and exhibitors to get us the best possible deal in terms of 
				release, number of theatres etc. So that part of the business 
				has got organised. The money that comes in from a producer is 
				more timely and organised since it is a corporate house which is 
				backed by very big names like UTV, Disney, Reliance, Sony, 
				Columbia Tristar etc.<br>
				<br>
				As most of these studios have a huge reputation, they try to 
				understand the logistics of film making and want their name to 
				be on a quality product, and this has made a huge difference. 
				Studios are backing different kind of films, and as they produce 
				a number of films, so they do not need to continue doing only 
				films that are seen as being commercially viable. So for example 
				they would do a Barfi and go ahead and back a Kahani, post its 
				making. It is an interesting place that corporates have come in 
				and they have thinned out our risks, by taking it upon 
				themselves so it makes good sense that they are working in the 
				film industry.<br>
				<br>
				<b>Indian Cinema is celebrating its 100 years. How have films 
				contributed to India’s socio-cultural milieu?<br>
				</b>Indian films have contributed not just to India, but as a 
				vehicle for India even abroad. People may think that it is not 
				important, but I believe that Indian cinema has played a huge 
				part in promoting the culture of India, and this does not just 
				mean cultural films but even mainstream movies like Kuch Kuch 
				Hota Hai, have popularised things like Karva Chauth (an Indian 
				festival) and concepts like friendship day. They have made a big 
				impact in the world and they reflect what India stands for -- 
				whether it is India shinning, growing, developing, doing well or 
				being a country which is wonderful place to be in.<br>
				<br>
				Our films embody all these elements, while this may not be true 
				of all the 900 films that are made. A couple of hundred films 
				from all over the country certainly stand for what the country 
				is going through. Movies reflect the times that we are in and I 
				believe that films of a country become popular when a country is 
				economically doing well. So you hear of Korean films, as Korea 
				is doing well. Similarly when Japan was developing rapidly, 
				Japanese films set the cultural tone of the region. The fact 
				that Indian films are being liked is not just because of the 
				Indian films but also because it is the country which is being 
				looked upon in a different light. The first thing that you 
				notice about a country when you are not familiar with it are 
				some cultural elements like its music, certain news snippets and 
				its films. The fact that Indian films are being liked globally 
				is because India is on the threshold of great development. <br>
				<br>
				<b>What do you look for in a movie before agreeing to be a part 
				of it? <br>
				</b>When I started my career I always thought that my role 
				should be important, but it changes as you go about. Then it 
				became the big names that I could work with. When you are 24-25 
				years, you think that you should have a big role so that you can 
				act well, then it becomes about working with the top names like 
				- Subhash Ghai, Yashji (Yash Chopra) or Mani Ratnam. But as you 
				develop your craft and continue in your art, what matters to you 
				is whether this is a story that you want to tell at this point 
				of time, in whatever capacity as a director or actor, and I am 
				speaking here only as an actor. The question I ask myself is -- 
				can I get like-minded people to tell the same story? And then 
				you start working with a director who at that point of time 
				would like to do something similar and there is a whole team 
				which is geared to making that film together. So you start 
				looking for like-minded people to work with even if the subject 
				is not of your choice. <br>
				<br>
				<b>You have gained a lot over the 20 odd years of your stardom. 
				Are there times when you feel that you have lost something on 
				the way?<br>
				</b>No, and I say this again and again. If I was given a choice 
				to live this life all over again and to have everything that I 
				have, I would give an arm and a leg also to have it. I know that 
				I got a lot more than I thought I would or even in my wildest 
				dreams believed that I can. The work that I have got to do has 
				been fantastic both in India and internationally. I have 
				beautiful children and a lovely wife. I love the work that I do, 
				it is seldom that you are in a line of work and you enjoy it 
				every morning even after so many years. I tell jokingly that I 
				go to office everyday at 10.30-11.00am and it does not seem like 
				work for me even now. My kids turn around and ask me, “don’t you 
				get bored?”, and I tell them, ‘I don’t.’ To be in the line of 
				work where I have dedicated half of my life and to enjoy it as 
				much as I did on the first day is the greatest gift and this is 
				more important than the money, name, fame and awards.<br>
				<br>
				<b>If you were to list down the three most important things in 
				your life, what would those be? <br>
				</b>The family comes first; there is nothing beyond my children. 
				I feel more peace and happiness when I am with my children. My 
				work is extremely important to me; acting turns me on. Unlike, a 
				lot of people who say that they can walk away from it, I cannot 
				walk away from acting, I enjoy the art of acting. I have a long 
				way to go, I don’t know whether I can achieve everything that I 
				want to as an actor in my lifetime, not in terms of name and 
				fame. I would like to see, feel and portray things in ways that 
				have not been done by me before. The third one would be just 
				ideas. I am in a space where I can give wings to ideas. Earlier 
				it was not possible as it was all about surviving, making money, 
				being material, setting up a house for my children, looking at 
				their education. I would like to take on all the ideas that I 
				have and get a set of like-minded people to work and build 
				something new -- like the IPL cricket team. These are not 
				businesses but something that I want to promote in my country 
				and which I can afford to do right now.&#8194;</font></td>
