<font face="Verdana" style="font-size: 8pt">United Press and Publishing, the 
publishing arm of United Media Services, has released the fifth edition of 
Connexion – Oman-India Business Review, 2013 coinciding with the 64th Republic 
Day of India. The book was unveiled before an august gathering at the Republic 
Day celebration ceremony at the Indian Embassy on January 26.<br>
<br>
The fifth edition of Connexion – Oman-India business relations 2013 brings to 
the readers the whole story of how Oman and India have moved beyond the 
bilateral clichés. As in the earlier editions, the articles in the Connexion 
includes sectoral reports, interviews and reports about the heightened business 
activity between the two countries.<br>
<br>
The socio-economic and political relationship between Oman and India is ever 
growing and the fantastic ties are reflected in the heightened business growth 
between the countries. Regular exchange of high-level visits has provided 
momentum to the political relations, and bilateral economic-commercial 
cooperations between the two countries are in the news all the time, smoothening 
the way for cross-border investments. With trade between the two countries 
crossing the $5bn mark in 2012, there couldn’t have been a better incentive to 
up the ante and set a new benchmark in the business relations. The magazine 
offers valuable insights on these achievements making it a must-have resource 
for businessmen and investors while taking strategic decisions.<br>
<br>
Connexion will be extensively distributed in Oman and India through established 
channels and will go a long way in creating awareness about trade and investment 
opportunities in both countries.</font></td>
