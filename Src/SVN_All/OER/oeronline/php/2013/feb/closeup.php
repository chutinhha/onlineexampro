<p align="justify" style="margin: 3px 10px">
<font face="Verdana" style="font-size: 8pt"><b>All Gulf Cooperation Council (GCC) 
countries except for the UAE suffered a drop in ranking in the 2012 version of 
the Corruption Perceptions Index (CPI). Berlin-based Transparency International 
issues the annual report by ranking countries, this time a total of 174 nations, 
based on perceived corruption.<br>
</b><br>
The index is produced based on data generated from numerous surveys and 
assessments, in turn carried out by some 13 internationally renowned 
establishments, the World Bank, the World Economic Forum and The Economist 
Intelligence Unit, to name a few. These surveys and assessments look into 
matters such as bribery of public officials, kickbacks in public procurement and 
embezzlement of public funds – certainly serious matters.<br>
<br>
Reviewed economies earn points on the basis of perceptions expressed by business 
and academic professionals concerning ways of doing business in various 
countries. The respondents, who include local and expatriate residents, provide 
views about possible corruptive practices involving public officials about 
winning business preferences such as contracts. This suggests that the report is 
not entirely objective as respondents express their perceptions subjectively.<br>
<br>
The CPI boasts a scale from zero to 100 points; as such, countries scoring below 
70 marks are required to assume actions to help bring integrity in the dealings 
of public officials. Against this assessment, all GCC countries need to carry 
out reforms. However, Qatar and the UAE, the best performers, scored 68 points, 
just below the differentiation line. Notably, the UAE managed to advance by a 
single to clinch a global ranking of 27, a position shared with fellow GCC state 
of Qatar. However, this meant Qatar conceding five notches in a single year, a 
considerable contraction. This puts Qatar and the UAE ahead of some European 
Union (EU) members, including Spain and Portugal.<br>
<br>
</font><b><font face="Verdana" color="#C00000" style="font-size: 9pt">
e-government services<br>
</font></b><font face="Verdana" style="font-size: 8pt">In effect, the UAE’s 
performance is indicative of the appreciation of entities doing the surveys and 
assessments of government efforts in combating wrong practices. The authorities 
seem determined to stamp out improper financial and administrative practices, 
thanks in part to increasingly relying on e-government services. Unfortunately, 
Bahrain saw its ranking fall by seven notches to position No 53 globally, 
thereby more than reversing gains made in the 2011 report. In fact, this level 
of performance is one of the worst in record for Bahrain, having secured ranking 
27 in the year 2003 at the height of comprehensive reforms initiated by the 
authorities. True, Bahrain maintained its status as the third best performer in 
the GCC, having overtaking this position from Oman in 2011. However, the retreat 
represents a setback to the country’s efforts in streamlining its image 
following the outbreak of the socio-political crisis in February 2011.<br>
<br>
Yet, Saudi Arabia, Oman and Kuwait experienced considerable drops in their 
rankings. For its part, Oman dropped by 11 notches to ranking <br>
<br>
No 61, certainly a cause of concern for the authorities. Kuwait saw its position 
plummet by 12 positions, the worst among GCC countries. One can hope that new 
parliament, only elected last week, would address the challenge of shaking up 
the image of misuse of public office.<br>
<br>
Likewise, Saudi Arabia needs to overcome its image headache, having dropped by 
nine notches, and thus sharing with Kuwait the 66th global ranking. Undoubtedly, 
this kind of standing does not fit the largest economy among the GCC and Arab 
economies at large. Certainly, GCC states cannot overlook the significance of 
CPI while seeking to attract foreign direct investment. &#8194;</font></td>
