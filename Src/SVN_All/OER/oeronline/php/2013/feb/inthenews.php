<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">In response to the Royal Directives 
of His Majesty Sultan Qaboos bin Said, the Small and Medium Enterprises (SMEs) 
Development Symposium was held at Saih Al Shamekhat in the Wilayat of Bahla 
under the patronage of Sayyid Khalid bin Hilal bin Saud Al Busaidi, Minister of 
the Diwan of Royal Court.<br>
<br>
The three-day symposium was aimed at putting the contents of the Royal speech at 
the Council of Oman on November 12, 2012 into practice, instilling 
entrepreneurship culture in Omani youths, linking education to the labour 
market’s requirements, diagnosing hindrances in the development of the SMEs and 
proposing solutions to the difficulties. Through the symposium, the government 
seeks to discuss the status of the SMEs in the Sultanate and the impediments for 
their growth. The outcome of the symposium will be utilised to draft policies 
for current and future five-year plans. Public and private establishments 
interested in supporting SMEs took part in the exhibition, which has showcased 
the experiences of entrepreneurs and provided start-ups with guidance by 
successful entrepreneurs and CEOs.<br>
<br>
Sayyid Khalid also inaugurated an exhibition on the sidelines of the event. 
“Programmes to follow up on implementation of the symposium’s outcomes will be 
held and recommendations made. Private and public sectors will ensure 
implementation of the plans,” he said.<br>
<br>
Sheikh Abdullah bin Nasser bin Abdullah al Bakri, Minister of Manpower, urged 
the youth to benefit from the opportunities available in the private sector. “It 
is important for the private sector to support entrepreneurs by adopting their 
ideas, encouraging them and providing them with a healthy environment for 
setting up projects,” he said.<br>
<br>
Sultan bin Salim al Habsi, secretary general of the Supreme Council for Planning 
and member of the symposium’s main committee, said, “His Majesty the Sultan has 
given great importance to encouraging business initiatives and SMEs, which are 
the main component of the economic diversification strategy, helping create job 
opportunities for Omani youths and enhancing the growth of the economy. It is 
important for the private sector to take part in economic activity and ensure a 
direct contribution to growth.”<br>
<br>
Khalil bin Abdullah Al Khonji, chairman of Oman Chamber of Commerce and Industry 
(OCCI), said the interest in the SMEs was growing. Currently, entrepreneurs 
represent 3 per cent of the 500,000 youths. <br>
<br>
He said that laws should be made simpler and fund should be increased to 
encourage youths to venture into such projects. He highlighted the role of 
education in the success of such projects.<br>
<br>
Al Khonji added that the government employees would be encouraged to process 
such projects quickly. “To this end, OCCI has set up a special department for 
SMEs, which will be supported by a committee of businessmen. It also created a 
department for communication among the government bodies and business owners. 
After the symposium, there will be an awareness campaign for the facilities 
provided by the Tender Board to the SMEs,” he added.<br>
<br>
The first session of the symposium touched upon entrepreneurship and free 
business culture and the role of education. It reviewed a number of case studies 
for entrepreneurs and discussed how to reduce the impact of hidden trade On the 
second day, the symposium discussed the policies, laws and procedures, as well 
as finance and investment. A number of ministers, undersecretaries, members of 
the State Council and Majlis Al Shura were present on the occasion.&#8194;</font></td>
