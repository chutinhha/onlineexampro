						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Enabling economic growth</strong></span><p class="style15" style="line-height: 17px">
				<img border="0" src="images/interview.jpg" align="left" hspace="6" vspace="6"><span class="style14"><strong><font size="2">The architect of 
				India’s National Payments Dr R B Barman joins Bahwan CyberTek as 
				Advisor. On his maiden visit to Oman, Barman shares his views on 
				the benefits of having a seamlessly integrated payment system 
				across the globe, in an interview with Mayank Singh</font></strong></span><p class="style15" style="line-height: 14px">
				<br>
				Dr RB Barman, is well known for his expertise in core areas of 
				statistics, econometrics, business information technology and 
				payment systems. He worked as an executive director in-charge of 
				these areas in the Reserve Bank of India (RBI) till 2008. Dr 
				Barman is now an independent adviser and director in public and 
				private sector companies. He is also associated with academic 
				institutions as adjunct professor, chairman of Research Advisory 
				Committee etc. He has recently joined Bahwan CyberTek as an 
				Advisor for its payments &amp; banking business. Excerpts from the 
				interview:<br>
				<br>
				<b>Your list of specialisations include statistics, business 
				information technology, econometrics and payment systems. How 
				did you acquire a specialisation in such multi-disciplinary 
				areas?<br>
				</b>I was driving IT initiatives in the Reserve Bank of India 
				(RBI) for eight years and as a result acquired a specialisation 
				in IT and payment systems. Statistics and economics are my 
				academic background and are also my core competency. This 
				combination of expertise provides certain advantages, as you 
				would get to understand in the course of our discussion. I 
				started as a statistician, joining the Indian Statistical 
				Service and from there in 1979 I moved to RBI at a higher level, 
				joining as a deputy director. The RBI was looking at 
				strengthening its research set up both in economics and 
				statistics, by lateral induction, and I joined the central bank 
				at the age of 31 and worked there for the next 29 years.<br>
				<br>
				After joining RBI, I thought that I should get a good grounding 
				in economics, so I took a break and went to IIT-Bombay, Mumbai 
				to do a PhD in economics, focussing on industrial economics. In 
				2000, I became the executive director, RBI as a part of that IT 
				and payments system became a part of my portfolio. For the next 
				eight years I was driving IT and payment system initiatives in 
				RBI. We took a lot of measures in the payment system field 
				during this time after the financial sector scam in India in 
				1992, as people took advantage of the time lag in settlements 
				and did kite flying. As a result we were planning to put in 
				place a more robust e-payment system – RTGS. I signed the 
				contract for RTGS in 2001 and when it was implemented in 2004, 
				we were a year ahead of China in implementing it. In the course 
				of time we created national retail payments infrastructure such 
				as NEFT, NECS, mobile payment (IMPS), Cheque Truncation System 
				etc., launched RuPay debit card. Electronic Benefit Transfer (EBT) 
				and Aadhar based biometric facility for authentication are other 
				major initiatives launched to transform retail payments 
				landscape in India. National Payments Corporation of India (NPCI) 
				has been set up as a not-for-profit company to create robust 
				infrastructure for clearing and settlement of retail payments, 
				apart from development of payment products for nation wide 
				rollout.<br>
				<br>
				In 1998, we started a data warehouse for the RBI; at that time 
				data warehousing was coming up for the private sector, but it 
				was at nascent stage in the central banks. The architecture of 
				data warehouse has online analytical processing capability 
				making it very powerful for analytical purpose. An ability to 
				sift data according to analytical needs by looking up, down and 
				across at ease, over time and space, is what a data warehousing 
				initiative is. This requires a data model, for organising the 
				data in a particular way, which gives users quite a bit of 
				flexibility to drill down, drill up and across so that one can 
				see a multi-dimensional view of a particular market and gain 
				much better insight on the forces driving the market and 
				associated risks. Even the most advanced central banks in the 
				world did not have such an advanced data warehousing system at 
				that time. When we implemented a data warehousing system in RBI, 
				I did a presentation in IMF and at the Bank for International 
				Settlements (BIS) and in both the places this was accepted with 
				a lot of enthusiasm. This was something new in respect of 
				subject orientation and associated data model and our 
				neighbouring countries started coming to us to understand our 
				model.<br>
				<br>
				<b>What prompted you to join Bahwan CyberTek as an Advisor?<br>
				</b>I visited Bahwan CyberTek in Chennai, and I was very 
				impressed with what they had done in the areas of national 
				payment systems for Middle East and Far East governments, the 
				quality of its resources and the expertise that it had built up 
				in payments &amp; banking solutions. We developed mutual respect and 
				admiration and formed this relationship, thanks to them, I must 
				say. Bahwan CyberTek is etching the next generation of payment 
				systems growing its Cuecent ePay platform to offer cutting-edge 
				solutions and payment business models; and it was felt that we 
				could together create this for their customers and enhance the 
				prospects to empower thought leadership and innovation, hence 
				this association. The globe is becoming a small place and we 
				should be in a position to participate in various parts of the 
				world to make the world a better place and IT is a very big 
				enabler for such initiatives. So I thought that coming on board 
				Bahwan CyberTek and seeing the work that they are doing will 
				give me a chance to see and understand the initiatives taking 
				place in this part of the world; and how we can align ourselves 
				in these as a part of a long run global integration process and 
				payment system is a wonderful initial step to achieve that.<br>
				<br>
				<b>Is a payment system model being used in India easily 
				replicable in different parts of the world?<br>
				</b>Very much so. India is already a part of the SAARC 
				initiative, which has been going on for the last five years. We 
				have been looking at integrating the payment system initiatives 
				in Sri Lanka, Bangladesh, Bhutan, Nepal, Pakistan, Afghanistan, 
				Maldives as a part of the SAARC initiative. This is a model that 
				can be extended to other countries. I believe that initially a 
				country develops a payment system that in the course of time 
				becomes a regional block, like you have a GCC initiative here. 
				Similarly, Europe has a SEPA initiative, North America has an 
				initiative involving the US, Canada and Mexico. South America 
				has an initiative, Japan and China have their own systems – 
				these initiatives are a natural process that a country 
				initiates. We can link these initiatives through technology to 
				achieve a seamlessly integrated global payment system and that’s 
				the vision that we would like to pursue globally and standards 
				which are being laid down.<br>
				<br>
				<b>Why is it important to seamlessly link various global payment 
				systems and does it present distinct advantages?<br>
				</b>Payment is fundamental for the banking and financial system. 
				When a person puts his money in a bank, the idea is that he 
				should be in a position to use that money whenever required and 
				this money used to happen in various ways in earlier days. Today 
				it is the age of electronic money, so you need to look at 
				e-money. Secondly, today people are moving all around the world 
				and there is movement in various ways – some come for jobs, 
				others for business or tourism. People are also increasingly 
				participating in the financial markets of other countries. If 
				India is growing fast, through rise in productivity and 
				innovation, its capital market may be in a position to attract 
				capital from other countries. Investors and companies would like 
				these investments to come in fast, necessitating the entire 
				cycle of clearance and settlement to happen quickly and we need 
				to integrate for such purposes. Then there is anti-money 
				laundering, which is an international commitment. We need to see 
				that money laundering and terrorism are under check. This is 
				important not only for an international system but also for 
				nations. In globalised competitive economy, capital flows to 
				places that provide the maximum growth potential, and to support 
				this there should be a provision for moving money from one place 
				to another seamlessly. All this is possible if there is a robust 
				state-of-the-art payment system. Governments are striving hard 
				to reduce currency (cash) in circulation as it has high 
				opportunity costs and associated risks. National payment systems 
				such as Cuecent ePay directly help address these issues besides 
				creating a shared services platform that aggregates country’s 
				needs for payments acquisition and fulfilment; and shared access 
				and delivery channels to achieve financial inclusion by bringing 
				all on board.<br>
				<br>
				<b>We hear a lot about Big Data and the challenge that it 
				presents. How far are we in a position to do adequate data 
				warehousing?<br>
				</b>Today, data warehousing tools have advanced quite a lot, 
				mainly because of a few things. First of all, the 
				computerisation of transaction processes in the last ten years 
				all over the world has enabled various institutions to capture 
				data at the transaction level. Various segments of the economy 
				are also getting computerised. Even while buying an asset one 
				has to do it electronically, thus enabling us to capture that 
				data on the asset side. All this allows one to look at data at a 
				micro level. I believe that we can develop a data warehousing 
				initiative – a kind of a micro-macro analysis framework for 
				economic analysis capturing the changes in distribution 
				underlying economic dynamics. This is a dream, but this is not 
				impossible ten years hence. We are building data block by block, 
				and if we are able to marshal the data at the micro and macro 
				level, we can sift through the process and forces in the economy 
				in a much better manner, helping us to understand causalities 
				and transmission channels better. This will give a much better 
				insight for formulation of policy and its evaluation.</td>
