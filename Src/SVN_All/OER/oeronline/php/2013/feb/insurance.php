<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">The The Capital Market Authority 
(CMA) has unveiled the e-insurance service for vehicles from the beginning of 
this year. As per this, the renewal of all vehicle licenses will be through an 
electronic system in which all insurance forms have to be uploaded by insurance 
companies. There will be no more yellow forms to be carried by individuals to 
the ROP to renew their vehicle registration. This way, one common database of 
vehicles can be shared by government authorities and the insurance companies in 
the Sultanate.<br>
<br>
As insurance companies and their intermediaries were coming to grips with this 
new system, the Oman Insurance Association (OIA) made arrangements to train the 
employees of insurers to ensure a smooth transition both for the companies as 
well as their customers. All front office staff were given training by OIA 
because from February onwards, there will be no physical ROP forms.<br>
<br>
The OIA’s role in minimising inconvenience to help companies cope up with the 
new e-system is a clear demonstration of the association’s objectives to act as 
a link between the insurance market in Sultanate and the Government and its 
concerned authorities, particularly CMA, in respect of all insurance legislation 
and their implementation.<br>
<br>
Established in December 2010, OIA is a trade body comprising insurance 
companies, insurance brokers and loss adjusters and others licensed by CMA to 
indulge in insurance activity.<br>
<br>
Spearheading the action to realise the objectives of OIA is the ‘Media and 
External Affairs Sub-Committee’, one of the six sub-committees formed over a 
year ago. This sub-committee comprises five members, with Lloyd East, CEO of Al 
Ahlia Insurance as chairman and the other four, namely Dr J Retnakumar, Bader 
Salim Mazrouki, Noura Mousa Balushi and Adil Abdullah Razaq, as members.<br>
<br>
The whole idea of forming the media sub-committee in OIA is to strengthen the 
Sultanate’s insurance industry by giving more thrust to the professional side. 
Individual companies will have their own strategies for developing their 
underwriting and claims management business. But OIA, through the 
sub-committees, will coordinate with all the companies in order develop a better 
understanding and cooperation and spread the message on the importance of 
insurance in all nooks and corners of Oman.<br>
<br>
Says Lloyd, “The insurance sector employs about 2000 people which is not a big 
number in comparison. But we do believe, we have a role in the development of 
the country. As His Majesty the Sultan has set out a vision for the development 
of the country, we feel that insurance just underwrites that vision. It insures 
confidence for businesses to grow; construction companies to build; and without 
insurance none of these can happen. We have a phrase at Al Ahlia which says we 
underwrite progress. And for progress to happen, we need insurance behind the 
scene. So I would say that the insurance sector is absolutely integral to the 
growth and development of the country. So there are fantastic opportunities for 
Omanis in this fast growing industry.”<br>
<br>
To spread this message all over the country, the media and external affairs 
sub-committee headed by Lloyd has drawn up a number of strategic plans including 
awareness programmes not only for insurance companies but also for the general 
public. All these plans will be put into action in the coming months.<br>
<br>
</font><b>
<font face="Verdana" size="2">Facilitator role</font></b><font face="Verdana" style="font-size: 8pt"><br>
The media and external affairs sub-committee is seeking to play the role of a 
facilitator in order to speed up the Omanisation of the insurance industry in 
the Sultanate. One way to do this is by encouraging the OIA members to focus on 
recruiting Omanis from various colleges and then sending them to CMA for ACI 
training. “We are still in the infancy stage and we have a lot of work on our 
hands,” adds Retnakumar.<br>
<br>
The committee has plans for holding workshops and seminars to achieve its goal 
of being a facilitator in not only popularising the job opportunities in the 
insurance industry but also training the industry employees itself on how to 
sell the products to its customers.<br>
<br>
Noura who has been in the insurance industry since the past 15 years says that 
apart from imparting information and raising awareness about insurance, training 
the staff of insurance companies and insurance intermediaries to sell the 
insurance policies on a holistic basis is very important. “Most often, the staff 
who are selling the insurance products are not able to explain the full details 
of the policies to the customer as they are only thinking about their own 
commission. Later, when it comes to the claim, the customer says that nobody 
told him/her about a particular point. When we train the staff to give full 
information to the customer, a customer will buy the product, this will then 
benefit the company and its employees.”<br>
<br>
Adds Lloyd, “we have several issues to tackle. Road safety is also an issue, 
training and Omanisation are others. What we thought we would do in our first 
conversation is to emphasise that we have a healthy industry and it is growing 
and the kinds of skills we need are quite professional. We are trying to raise 
awareness that this is a great industry and we are looking for talent. The more 
the companies look for better talent, the more better people we will get and the 
industry will thrive as there is underlying buoyancy due to the good economic 
conditions.”<br>
<br>
</font><b>
<font face="Verdana" size="2">Local talent<br>
</font></b>
<font face="Verdana" style="font-size: 8pt">One of the main objectives of this 
committee is to educate Omani nationals on the employment opportunities in the 
insurance industry. In 2012, the stipulated Omanisation percentage for the 
insurance industry was 65 per cent and now it has been raised to 75 per cent. 
All companies have their own programmes and strategies to achieve these targets 
and many of them have more than 65 per cent Omanisation.<br>
<br>
Talking about the present demand for human resources in the insurance industry, 
Lloyd says, the growth rate forecast is 17 per cent per annum for the next three 
years. Whether that will translate into 17 per cent growth in employment 
opportunities depends upon the efficiencies of businesses, upon what type of 
business you are involved in.<br>
<br>
“We can’t tell you how many people we need but we can tell you that the good 
news is that spending in infrastructure and investment in SMEs are happening now 
as per the directives of His Majesty. This will continue to fuel the insurance 
sector and that will reflect in the growth of companies and will lead to more 
job opportunities,” Lloyd adds.<br>
<br>
Building on Lloyd’s comments, Retnakumar says, “we should see to it that Omanis 
are placed in various top positions and not just on the front desk. So proper 
training and education to Omani nationals and equipping them to take up more 
responsibilities in the coming years is very important. In this direction, we 
should appreciate the efforts of CMA which has lot of initiatives for training. 
The CMA has already decided to fund the Omani trainees who are appearing for the 
Association of Chartered Insurers examinations.”<br>
<br>
Retnakumar furhters tha they are proposing to insurance companies to target 
college graduates through campus selections and train them. Because insurance is 
not just economics and commerce, it covers everything under the sun. “We want 
people with different backgrounds including economists, risk engineers, 
medically trained people,” he adds.&#8194;</font></td>
