		        <td valign="top" width="100%" colspan="2"> 

<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt"><b>Can you tell us what brings you 
to the Sultanate?</b><br>
I am here with a business delegation of some 16 people. There is a very strong 
banking representation in the group and also accountants and lawyers, maybe 
reflecting the city of London and its strengths which is banking and insurance 
law and accounting and it is this cluster which is really has made London such a 
success in global financial centre terms.<br>
<br>
In the year 2000, financial services accounted for 5 per cent of UK’s GDP and 
last year it is risen to 9 per cent of GDP so in these 12 years it has almost 
doubled and if you add in professional services like law and accounting, it 
comes to just under 15 per cent of GDP. So, that’s countrywide. So of course we 
want remind people of how strong London is. There are many services that we can 
provide to Omani companies and banks. We are here to explore opportunities. Many 
of my colleagues are quite experienced with Oman and they are looking and 
smelling the air and seeing what is happening and they are interested in getting 
up to date.<br>
<br>
<b>Did you have any talks with Omani officials?<br>
</b>We met with the Central Bank of Oman officials, the Minister of Commerce and 
Industry, the Deputy Prime Minister and we met with the HE Yahya Al Jabri, 
chairman of Duqm SEZ. I think my overall impression is that there is a change in 
the Omani society. We are seeing a desire to have more investments in SMEs and 
in the private sector. We had a long discussion with the Minister of Commerce 
about privatisation and how it can be achieved. We talked about financing 
methods and the rise of Islamic banking and you have two new Islamic banks. 
Nothing very dramatic from your point of view but very interesting for us.<br>
<br>
<b>As a banker, can you shed light on the 2008 period when everything went 
topsy-turvy?<br>
</b>I think we must remember it was a global financial crisis. Meaning, we had 
accumulated huge assets in China and other Far East countries against huge debts 
in Europe, UK and the US. Some countries were clever like Canada, Australia, 
Scandinavia, as they did not over-borrow in this period. Japan was already 
heavily borrowed. <br>
<br>
From that position of enormous assets on this side (east) to enormous debts in 
the west, something had to break. And of course like in every financial crisis 
since the past 500 years, it’s the banking sector that breaks first. And because 
they are so many major banks in London, it was of course the focal point for 
some of these breakages. But, it’s also good to remember that the financial 
crisis started in 2007 when liquidity dried up and banks stopped being able to 
find the money to lend to each other.<br>
<br>
And that in a way was the crucial breaking point for the imbalance. And it’s 
really that then which produced the credit difficulties, which then produced 
severe banking difficulties at Lehman Brothers and others and then we have seen 
the results of that in the last few years.<br>
<br>
I should also add that Oman and other GCC countries have been clearly away from 
that as they have kept their balance sheets in much better position helped by 
the oil prices and also by having a more sensible economic approach during this 
period.<br>
<br>
Back to 2008, of course we have learnt a lot since then and much has changed. 
Some of the major imbalances are there, we mustn’t forget that. And there is 
still much to be done to sort out European debt which is still a difficulty in 
many countries, particularly in the south. But since then, there have been many 
changes in the banking regulations like changes in capital requirements, changes 
in liquidity management, portfolio management and changes in remuneration 
practices. I mean, there are huge changes actually in my industry. And I think, 
that has produced a change in culture too.<br>
<br>
<b>What could be the single most important change that has happened which could 
prevent a similar crisis in the future?<br>
</b>Ah… What a question (repeats). It’s experience. The reason why the Canadian 
and the Swedish banks did not have a problem now was that they had a problem 
back in 1991-92. They had their crisis then, severe crisis particularly in 
Scandinavia. The banks did not go bust but got into difficulties and that 
experience gave them so much learning for the present. I worked for a Swedish 
bank for 30 years and the change that was brought about in 1992 meant that we 
did not do proprietary trading on our own behalf since 1992. We do property 
lending above a limited percentage since 1992. <br>
<br>
We have much different credit portfolio management systems since then. We do not 
own an insurance company, since then, etc and etc. Many such conservative 
measures were brought which have seen us through the last few years.<br>
<br>
<b>What about London? Has it been able to pick up the pieces since 2008 and move 
forward?<br>
</b>Yes I think so. Actually I am not sure if it ever lost the pieces but the 
reputation has been damaged. Actually if you look at the net contribution of 
London to the GDP, taxes paid (corporate and income), they never lost it. Some 
employment has come down in the banking sector for sure. Some of the income tax 
GDP has come down. We have seen a flood of money coming to London from France, 
Spain and other overseas. So the property market is buoyant in London. It’s 
interesting. In two more years, I will come back and tell you a better 
understanding of the past than now, but today it feels like London has been hit 
but it’s in a much better position than say Paris, Madrid or New York. In 
London, we are very critical. We open up our books to a lot of analysis. We 
believe we have a much better system than before.<br>
<br>
<b>You were probably in the thick of things then. Did you perceive it as a kind 
of a doomsday scenario?<br>
</b>No, never a doomsday scenario but a real crisis. I think what started off as 
a liquidity crisis became a banking crisis. Now that banking crisis is over and 
now it is sovereign debt situation.<br>
<br>
<b>What is the way forward? Is there a light at the end of the tunnel for this 
whole financial crisis?<br>
</b>There is a quick scenario and a slow scenario. The quick one is we can 
persuade Chinese money to come in and invest in Europe. It’s happening a bit. 
They are buying car plants and a quite a lot of other industry because it 
immediately brings the balance sheets onside. This way, you increase the growth 
and prosperity of citizens and they pay more taxes and decrease the sovereign 
debt which could then be solved maybe as quickly as in the next five years. <br>
The slow way is that we painfully export our way back to good health and that 
takes longer.<br>
<br>
<b>Would you say that money is the root cause of all evil?<br>
</b>No, no, no… It’s not money, it’s the love of money that is the root of all 
evil. But not money itself. Money is a commodity. Like air, money is important. 
Buts it’s all about having the right place for money in life. We should not love 
money. We should use money for good purposes. Love your fellow human being, but 
don’t love money.<br>
<br>
Faith is more important than money. Because at the end of the day it is what you 
believe in, whether it’s a product or a service or in a way of life or in the 
society. It’s more than money.<br>
<br>
<b>You are advocating that London should become the financial hub once again</b><br>
I think London is (emphasis) the financial hub actually. While things are tough 
in France and in Europe, London is the natural gateway to Europe, we could say. 
Look at international law, it is based on the English law. It’s fantastic and 
it’s a huge strength. So you have centers like Leeds which is really thriving in 
the legal area. Then you have Scotland which is very much thriving in asset 
management and asset management services. Equally we have great maritime sector 
in places like Liverpool and Glasgow where we have expertise developing which is 
actually very useful export commodity for countries like Oman.<br>
<br>
And I think we are increasingly looking at the UK for very specific sectoral 
strengths like manufacturing, etc. London could become the hub and spoke model 
and I truly I think, in this timezone, London has a great role to play and it 
will remain as the driver.&#8194;</font></td>
