				<b><font face="Verdana" size="2">
				Alderman Roger Gifford, Lord Mayor of the City of London 
				2012-13, was in Oman with a business delegation having a very 
				strong representation from the banking industry. Gifford himself 
				is a banker, heading the London office of Skandinaviska Enskilda 
				Bank (SEB). Born in St Andrews, Scotland and educated at 
				Sedbergh and Trinity College, Oxford, Gifford is currently the 
				vice chairman of the Association of Foreign Banks in London, the 
				financial hub of the world In an exclusively candid chat with 
				Visvas Paul D Karra, Gifford speaks about his experiences and 
				outlook for the world’s financial sector</font></b></p>
