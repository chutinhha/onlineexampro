	<tr>
		        <td valign="top" width="100%"> 

<p align="justify" style="line-height: 14px; margin: 10px">
				<img border="0" src="images/technology.jpg" width="525" height="256"><p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt">Oracle has been creating a massive 
build-up for its OracleCloud with significant capital investments in software 
and hardware for years in order to bring reliable, secure and enterprise grade 
services to its customers. All this came with an aim of providing the best 
solutions to customers for cost savings and driving performance at the same 
time. Oracle’s cloud solutions provide the industry’s broadest and most complete 
portfolio of public, private and hybrid cloud to deliver a broad suite of 
subscription-based, enterprise-grade application services, platform services, 
infrastructure services and social services.<br>
<br>
Mark Hurd, president of Oracle Corporation, highlighted Oracle’s enthusiasm to 
provide the latest technology in cloud computing in his address at the company’s 
maiden roadshow on cloud computing known as Oracle CloudWorld in Dubai in 
January. Hurd said Cloud computing with the help of the latest transformational 
technologies can be imperative to organisations, large and small, across any 
region or industry.<br>
<br>
“Oracle is bullish on Cloud and has been for some time,” Hurd says while 
pointing out that Oracle is five years ahead of its peers in the industry with a 
range of cloud services applications like human capital management, talent 
management, sales and marketing, customer experience, financial management, 
procurement sourcing, inventory, project management and governance risk, 
compliance, etc.</font><p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" size="2"><b>Investing in cloud<br>
</b></font><font face="Verdana" style="font-size: 8pt">To queries about 
investments, Hurd stated that in this fiscal year, the corporation has invested 
$5bn in cloud computing and in the past three years, the company’s figure in 
research and development of cloud has touched $13bn.<br>
<br>
Hurd explained that some of the acquisitions like RightNow Technologies taken 
over for $1.5bn and Taleo Corporation for $1.9bn are designed to enhance cloud 
solutions. Oracle said it also would buy Collective Intellect, which tracks and 
analyses customers’ comments on social media.<br>
<br>
Last month, the company also agreed for the buyout of DataRaker to expand in 
smart-grid analytics platforms that enable water, power and other utilities to 
use data to increase efficiencies.<br>
<br>
In his key note address to the delegates from 20 different countries, Hurd 
detailed Oracle’s strategy and the trends driving cloud. He said there are nine 
billion Internet devices in the world, which will balloon to 50 billion by 2020.<br>
<br>
Stating that the number of Internet users today exceeds two billion worldwide, 
he said global Internet penetration is estimated to grow from 35.3 per cent in 
2012 to 42.8 per cent in 2016. Around 90 per cent of all Internet users now have 
an account on at least one social service, and there are more than 13 million 
business pages on Facebook alone.<br>
<br>
The Oracle president said that the data being generated worldwide will be 44 
times greater in 2020 than it was in 2009, reaching a worldwide total of eight 
trillion terabytes by 2015. Hence, Hurd added, companies are searching for 
solutions that will provide the unprecedented capacity, scalability, and speed 
to keep pace with that explosive growth.<br>
<br>
To counter this massive data explosion, Oracle has introduced new hardware that 
it developed with Sun technology. Hurd said the company has unveiled two 
computer systems, one with faster data-analysis capabilities and another for 
organising information from the Web. Sales of Oracle’s Exadata, Exalogic, 
Exalytics and other engineered systems more than doubled and the new cloud 
business is also approaching a $1bn annual run rate.<br>
Exadata X3 is designed to keep all of your databases in-memory with a single 
rack enabling up to 4TB of DRAM and up to 22TB of NAND flash memory, with the 
database compressed ten times. This will enable a single machine to accommodate 
up to 220 terabytes of databases in memory. Exadata X3 now allows write 
capabilities from flash, making writes 20 times faster at the same price, and 
consuming less power.<br>
<br>
</font><font face="Verdana" size="2"><b>Regional interests<br>
</b></font><font face="Verdana" style="font-size: 8pt">Oracle has strong 
relationships in the Middle East and is continuing its investments in developing 
local expertise in the field of software engineering. The company has a number 
of education and skills development partnerships with government and private 
organisations in Saudi Arabia, UAE, Oman, Bahrain, Kuwait and Egypt, as well as 
other countries in the region.<br>
<br>
Hurd says there is high interest from government and private organisations in 
the region to invest in cloud computing, which will help them run smooth, safe 
and profitable operations.<br>
To strengthen its commitment to the region, Oracle has recently moved to its new 
building in Dubai Internet City, which will help accommodate its continued 
growth of operations and the increasing number of employees.<br>
<br>
Meanwhile, Alfonso Di Ianni, Oracle’s senior vice-president for Eastern Central 
Europe, Middle East, and Africa (ECEMEA), who also addressed the CloudWorld 
roadshow said that Oracle is in contact with the Sultanate’s Ministry of 
Education and is ready to offer its various education and skills-development 
programmes.<br>
<br>
Di Ianni also pointed out that the corporation is offering internships to 
talented Omanis in the field of software engineering, in collaboration with 
universities in the Sultanate.<br>
<br>
In addition, the Oracle Academy is also focusing on nurturing the software 
engineers of tomorrow in the UAE, Saudi Arabia, Kuwait, Bahrain, Egypt, and 
other parts of the region, he added.<br>
<br>
The company, Alfonso elaborated, is also pursuing regional skills-development 
partnerships with private organisations. He noted that there is significant 
interest from the government as well as private organisations in the region to 
invest in cloud computing.<br>
<br>
</font><font face="Verdana" size="2"><b>Cloud to fuel growth<br>
</b></font><font face="Verdana" style="font-size: 8pt">Oracle will rely on cloud 
computing to fuel growth by relying on hardware sales and its cloud-computing 
service to broaden use of its products. The company had already gobbled up more 
than 70 companies in a $40bn buying spree in recent years.<br>
<br>
And now, it will focus on what it already has including the Oracle Cloud, which 
delivers software online via cloud computing. Today, Oracle has over 10,000 
customers and more than 25 million users of the Oracle cloud.<br>
<br>
In addition to the public cloud services, the company also has the Oracle 
Private Cloud, which is designed to take Oracle’s cloud service but run it on 
hardware Oracle owns and controls with a company’s on-premises data center. <br>
<br>
Since Oracle builds its own infrastructure and software, security of data is not 
an issue, Hurd says. “The number one issue that we have in the Middle East is 
finding great people,” he adds. To overcome this, the company is recruiting 40 
per cent of interns from the region.&#8194;</font></td>
