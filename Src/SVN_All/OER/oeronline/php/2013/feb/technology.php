		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; line-height:17px"> 
				<b><font face="Verdana" size="2">
				Oracle, the world’s second-largest software maker, chose Dubai 
				to launch its global CloudWorld roadshow to introduce the 
				company’s expertise in ‘cloud’ technology and its benefits to 
				business and public and private sector industries. The 
				CloudWorld is being hosted in major cities around the world 
				including Frankfurt, London, Los Angeles, Mexico City, Mumbai, 
				New York City, Singapore, Sydney and Tokyo. Visvas Paul D Karra 
				joined other journalists from the Eastern Central Europe, Middle 
				East and Africa (ECEMEA) in Dubai to cover the event which 
				attracted more than 1500 IT delegates. A report</font></b></p>
