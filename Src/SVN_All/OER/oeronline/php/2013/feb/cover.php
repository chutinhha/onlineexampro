					sustaining a high growth rate. As the benefits of high 
					growth rate percolates downward, the trickle-down effect of 
					the high growth rate will lead to increase in income and 
					spending power of the masses. Moreover, the focus of the 
					Government on creating additional employment for the 
					national workforce through the provision of 56,000 jobs in 
					2013 will benefit the younger generation,” says Raisi(See 
