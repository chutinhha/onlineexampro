						Bigger, bolder, better</font></b><p class="style15">
				<span class="style14"><strong><font size="2">The State General 
				Budget 2013 is ambitious and has the biggest ever spending 
				directed towards the Omani citizens for education, training 
				social security and housing. And there is a commitment from the 
				government to continue with its mega projects which aim to 
				create jobs for Omanis. Visvas Paul D Karra delves into the 
				budget for this report</font></strong></span><p class="style15" style="line-height: 14px">
				<img border="0" src="images/cover01.jpg" width="200" height="176" align="right" hspace="4" vspace="4"><p class="style15" style="line-height: 14px">
				<span class="style14"><font style="font-size: 8pt"><strong>What 
				has Islamic banking got to do with the fiscal budget of Oman? 
				Plenty of good news, would be a fair answer. If you want to run 
				the risk of being labelled a partisan, you could go ahead and 
				add, Islamic banking is a master stroke at the right time by the 
				government. But how does it all add up to the fiscal numbers?<br>
				</strong><strong style="font-weight: 400">The statement by HE 
				Darwish Al Balushi, Minister Responsible for Financial Affairs, 
				while announcing the State Budget for 2013, that the deficit of 
				this year’s budget will partly be financed through issuing 
				development bonds in the market tells us from where it is all 
				coming. Experts say that this will happen in the form of sukuk.<br>
				<br>
				The minister goes on to say that issuance of development bonds 
				will encourage domestic savings and enhance the efficacy of 
				utilising the domestic savings in the local market. The 
				Government is currently considering the sale of some government 
				shares in wholly or partially government owned companies through 
				MSM, which basically means divesting some stake.<br>
				<br>
				The between-the-lines script is that this two-pronged strategy 
				by the government will open up the cash boxes of shariah-compliant 
				unused funds to lap up the development bonds as well as the 
				government divestment stakes. Lo and behold, your Islamic 
				banking is in full play as according to the Islamic banking 
				regulations, shariah-compliant funds cannot be channeled into 
				non-islamic banking operations. It means they need new 
				investment options to generate returns for the millions of rials 
				which will be raised through domestic finances by the Islamic 
				financing institutions. <br>
				<br>
				The sub-plot in this brilliant story is that the government is 
				killing two birds with one stone. Firstly, it is giving a boost 
				to Islamic banking by partly financing its deficit from the 
				local market and secondly the government divestments will 
				stimulate the capital market into enhanced activity and 
				liquidity and encourage new IPOs.</strong></font></span></td>
