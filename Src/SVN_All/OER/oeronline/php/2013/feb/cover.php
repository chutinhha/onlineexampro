                    <hr color="#C0C0C0" size="1">
					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>Quick look<br>
					</b></font>Some of the key statistics (see table) of the 
					State General Budget 2013 revealed by HE Darwish include a 
					significant -- 29 per cent increase in expenditure to give a 
					fillip to the economy sending a message that the government 
					will not curtail necessary expenditure.<br>
					<br>
					The minister disclosed that the total revenues of the state 
					for the year 2013 were estimated to be about RO11.2bn 
					against RO8.8bn in the budget for the year 2012, with an 
					increase of RO2.4bn, i.e 27 per cent. The oil revenues 
					constitute 84 per cent of total revenues. <br>
					<br>
					The oil revenue has been calculated on the basis of $85 per 
					barrel and a daily production rate of 930,000 barrels, 
					therefore the oil revenues were estimated to be about RO8bn, 
					which represents 72 per cent of the total revenues. The gas 
					revenues, estimated at RO1.3bn, represents 12 per cent of 
					the total revenues.<br>
					<br>
					The non-oil revenues were estimated at about RO1.8bn with an 
					increase of 13 per cent over last year, which represent 16 
					per cent of the total revenues.<br>
					<br>
					There is a seven per cent growth in economy budgeted (same 
					as in 2012) which is higher by one per cent compared to the 
					eighth Five Year Plan annual average GDP growth. <br>
					<br>
					Humayun Kabir, general manager, wholesale banking, National 
					Bank of Oman, says the growth oriented budget reflects the 
					healthy finances of the treasury and a determined effort to 
					keep turning the wheels of the economy in an otherwise 
					globally challenging economic environment. Deficit at 5 per 
					cent of GDP is a tad on the higher side but that may not be 
					an issue if oil prices remain north of $100.”<br>
					<br>
					Although a deficit of RO1.7bn is budgeted, as in the 
					previous years the borrowings are insignificant. Much of the 
					budgeted deficit will be met out of the last year’s surplus 
					and the rest from development bonds. The deficit, although 
					higher than last year’s budget is within acceptable limits – 
					5 per cent of GDP. The minister did clarify that in order to 
					avoid any deficit, the breakeven price for oil should be 
					$104 per barrel as compared to the realised 2012 price of 
					$109 per barrel.<br>
					<br>
					The significant growth in budgeted expenditure has been 
					possible because of the high oil prices, $109 per barrel 
					compared to an average of around $60 per barrel used in 
					preparing the eighth Five-Year Plan.<br>
					<br>
					Defence and security expenditure budget of RO3.5bn is more 
					than double the expenditure estimated in the Five Year Plan 
					(approximately RO1.7bn). The increase in budget for defence 
					and security in 2012 was 57 per cent and in 2013 there is 
					another 38 per cent increase.</td>
