                    <hr color="#C0C0C0" size="1">
					<p class="style15" style="line-height: 14px">
					<font color="#333333" size="2"><b>Encouraging trends<br>
					</b></font>
					<img border="0" src="images/cover03.jpg" align="right" hspace="4" vspace="4" width="200" height="176">Ashok Hariharan, partner and head of Tax, Oman &amp; 
					United Arab Emirates, KPMG, says the actual growth in GDP 
					achieved in 2012 is estimated at 8.3 per cent which is 
					higher than the 2012 budgeted growth of 7 per cent. This 
					indicates that despite the global and regional challenges, 
					the Omani government has been able to drive the economy 
					forward. Another encouraging trend is the confinement of 
					inflation in 2012 to three per cent compared to four per 
					cent in 2011. The target inflation rate in the 8th Five-Year 
					Plan is 4 per cent.<br>
					<br>
					According to Hariharan, another indication of growth in the 
					economy is the performance of the MSM listed companies. The 
					Minister Responsible for Financial Affairs mentioned that 
					the performance of companies listed in MSM has increased by 
					18 per cent although the market index rose only by two per 
					cent suggesting that 2013 could be a strong year for 
					equities. The market will also be buoyed by the proposed 
					sale by government of its shares in government owned 
					companies.<br>
					<br>
					“Whilst indications are good for the future particularly if 
					current level of oil prices is maintained, the minister has 
					rightly cautioned for the need to control expenditure within 
					the budgeted amounts. Further he has stressed on the need to 
					reduce subsidies which are quiet large and amount to 
					RO1.3bn,” Hariharan adds.</td>
