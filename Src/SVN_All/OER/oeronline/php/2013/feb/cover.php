					<p class="style15" style="line-height: 14px"><b>
					<font size="2" color="#333333">The sukuk way<br>
					</font>In his budget speech, HE Darwish expressed hope that 
					the Muscat Securities Market would witness improvement, 
					during 2013 as a result of the continuing Government 
					spending which will support the issuance of the anticipated 
					Islamic financial instruments to give the market a 
					substantial depth and additional financial instruments and 
					will enhance the confidence of the investors.</b><p class="style15" style="line-height: 14px">
					<img border="0" src="images/cover02.jpg" width="200" height="176" align="right" hspace="4" vspace="4">Reflecting on the minister’s statement, HE Abdullah Al 
					Salmi, the executive president of the Capital Market 
					Authority (CMA) says, “We hope that the government will come 
					up with new products like sukuk to achieve economic goals 
					like creating a benchmark yield curve for the Omani rial and 
					secondly use the capability of the market to finance the 
					mega projects that are coming up in Sohar, Salalah, Duqm as 
					well as the road and railway infrastructure and other 
					utility projects. That will ease the financing part from the 
					government. Also products like sukuk are definitely required 
					because the Islamic banks as well as regular banks with 
					Islamic banking windows need to utilise their funds 
					somewhere. If sukuk can finance <span class="style14">
					<strong style="font-weight: 400">
					<font style="font-size: 8pt">the</font></strong></span> 
					deficit in the budget, the surpluses that are generated from 
					oil revenues can be invested in the SGRF (sovereign fund) to 
					get more returns.”<br>
					<br>
					The government could divest its stake in some industries and 
					downstream oil and gas projects, and even from Omantel in 
					which the government still holds about 70 per cent, says HE 
					Salmi. This will add liquidity and depth to the market which 
					he thinks will give good sentiment to the market. What will 
					really happen is that the proceedings that come from 
					divesting stake could be used to initiate new projects. This 
					would enhance the economy and create new jobs and as a 
					result it will benefit the capital market’s liquidity and 
					add depth to the market and encourage new projects from the 
					private sector.</td>
