<p align="right" style="margin-left: 10px; margin-right: 10px; margin-top: 0; margin-bottom: 10px">
<i><font face="Verdana" color="#008080" style="font-size: 8pt">By Oliver Cornock</font></i></td>
		</tr>
	<tr>
		        <td valign="top" width="100%"> 

<p align="justify" style="line-height: 14px; margin: 10px">
<font face="Verdana" style="font-size: 8pt"><b>Despite envisaging a budget 
deficit of $3.1bn for 2012, the high price of oil has meant the Sultanate is set 
to enjoy a comfortable surplus again. This is news in itself, but all eyes are 
now turning to 2013. What lies in store? And how will the surplus be spent?</b><br>
The Sultanate’s answer to these questions is clear. In addition to the stated 
goals of the current Five-Year Plan, and initiatives announced in 2011, the 
government wants to offer a boost to the estimated 100,000 small and 
medium-sized enterprises (SMEs) operating in the country, as well as encourage 
new SME start-ups. This will enable the private sector to play a bigger role in 
driving economic growth and creating jobs – a particularly pressing issue since, 
every year, around 17,000 Omanis graduate and enter the job market.<br>
<br>
</font><font face="Verdana"><span style="background-color: #EFEFE9">
<font color="#800080" style="font-size: 10pt"><b>Three key areas<br>
</b></font></span></font><font face="Verdana" style="font-size: 8pt">Broadly 
speaking, the government has identified three key areas that will assist the 
development of the SME sector: first, the need for new regulations that are more 
SME friendly; second, increased – and more efficiently targeted – financial 
assistance for start ups; and third, improved training and entrepreneurial 
skills education.<br>
<br>
The SME sector has tremendous potential for growth. However, it is fair to say 
that the potential has long lain dormant. It has been estimated that about 80-90 
per cent of start-up businesses fail in Oman due to undercapitalisation and poor 
management resources. Moreover, starting up a company in Oman has traditionally 
been very difficult, sometimes taking up to 12 months. Add to that the perceived 
unattractiveness of the private sector – most Omanis would still prefer a job in 
the public domain – and one can understand why SMEs contribute 16 per cent of 
Omani GDP, compared to over 50 per cent in the UAE, according to Qatar 
Development Bank statistics.<br>
<br>
More positively, the budget surplus has led to a renewed effort to address these 
issues, and 2012 has already seen number of landmark developments come into 
effect. In June, the Ministry of Commerce and Industry (MoCI) revised the 
definition of SMEs, in an effort to improve the flow of credit and provide more 
efficient training and guidance to SMEs. Under the previous definition, the 
number of employees was the only criteria used to define a company as a small or 
medium enterprise. The new definition, in line with international benchmarks, 
takes into account the annual sales turnover of businesses. It also includes a 
category for micro-sized enterprises, and it is hoped this new classification 
will enable the government to provide stronger and more effective support to 
SMEs.<br>
<br>
</font>
<font style="font-size: 10pt; background-color: #EFEFE9" face="Verdana" color="#800080">
<b>Reluctance to lend<br>
</b></font><font face="Verdana" style="font-size: 8pt">Indeed, the difficulties 
involved in obtaining start-up capital for micro and small businesses is a 
second factor that has long stifled SME growth, an issue not only here in Oman, 
but also elsewhere in the GCC, where banks remain largely reluctant to lend to 
SMEs. Research conducted as part of the Regional Oman Economic Association 
Conference in January 2012 saw respondents agree the process of obtaining 
financial assistance was too long. And, according to a 2008 study by business 
information firm Dun and Bradstreet, banks in the UAE rejected 50-70 per cent of 
credit application from SMEs that year, due to a perceived high level of risk 
and applicants’ failure to meet loan conditions.<br>
<br>
Yet this situation now looks set to change in the Sultanate. The government is 
not only looking to increase its spending and financial support for the sector, 
it also wants to take advantage of international expertise to identify how that 
money should most effectively be allocated. In November, Bank Muscat and the 
International Finance Corporation (IFC) signed an agreement to facilitate 
financing access to SMEs, in order to help them expand and create jobs. Within 
this partnership, the IFC will advise the bank as it launches new products and 
services aimed at micro, small, and medium enterprises. Furthermore, Bank Muscat 
will also offer support to women-owned businesses, helping them become more 
active players in the economy.<br>
<br>
The second measure will see SMEs enjoy some RO100m of increased funding, that 
will meet around 50 per cent of the financing needs of existing and new SMEs at 
subsidised interest rates. Launched in April, the SME Development Fund will make 
investments via private sector pension funds, financial institutions and large 
business groups, distributing government grants towards financing Omani 
entrepreneurs, as well as providing training and mentoring.<br>
<br>
The general picture then is that, across the board, the government is looking to 
SME development with a new vigour. Earlier this year, for example, the 
Telecommunications Regulatory Authority (TRA) unblocked a number of Voice over 
Internet Protocol (VoIP) services, a move that paved the way for cheaper 
international communication and providing a boost to SMEs, helping them to keep 
costs down in the early stages of their growth.<br>
<br>
</font>
<font style="font-size: 10pt; background-color: #EFEFE9" face="Verdana" color="#800080">
<b>Developing skills base<br>
</b></font><font face="Verdana" style="font-size: 8pt">Of course, while the 
success of the SME sector is in large part reliant on regulation and funding, it 
is unlikely that Oman will see a rise in the number of privately-owned business 
without working to further develop the skills base, something that the 
authorities are aware will take time. In the aforementioned research as part of 
the Oman Economic Association Conference, it was found that Omani financial 
institutions still believe that the quality of business plans, coupled with the 
business owners that represent them, are generally “very poor”, with a general 
weakness in entrepreneurial capabilities amongst locals.<br>
<br>
To counter this, and to spur SME growth, the Ministry of Commerce and Industry 
agreed in August 2012 to expand the role of Sanad – its SME incubator programme. 
The programme has raised the ceiling of loan funding, as well as the age-group 
of prospective entrepreneurs. The new services are aimed at diversifying the 
national economy by fostering entrepreneurship. To achieve this, Sanad raised 
the upper limit of its loan programme for start-ups to RO50,000 – a move which 
Ibrahim bin Said al Alawi, Sanad’s Executive Director hopes will spread a 
“culture of free business.”<br>
<br>
In addition, more emphasis is to be laid on women’s training. While the 
participation rate of women in the labour force remains low, there has been a 
marked improvement in the availability of education and training. The women’s 
training programme launched by Sanad, for example, offered training in tailoring 
to 473 women in the months leading up to August 2012. Overall, around 27 per 
cent of the beneficiaries of Sanad funding are women.<br>
Going forward, it will take all three aspects of this SME development plan – 
business-friendly regulations, adequate funding and a healthy dose of 
entrepreneurial spirit – to get the sector flourishing and meeting its true 
potential. The current moves are a significant step in the right direction, 
though it will, of course, take time for them to bear fruit.&#8194;</font></td>
