                co-founded by Indian tech star Rakesh Mathur, makes that 
                possible. What more, the service comes free.<br>
                <br>
                Webaroo, the venture floated by Mathur along with fellow 
                computer hotshots Bradley Husick and Beerud Sheth, formally 
                launched the `Searchable, Offline Web’.<br>
                <br>
                “At home or on the go, in the air or on vacation, users can now 
                find the information they need, wherever they are,” said 
                Webaroo’s CEO Mathur, who made a fortune during the dotcom boom 
                by selling his hugely successful junglee.com to Amazon.<br>
                <br>
                The Indian face of Webaroo is not restricted to Mathur and Sheth, 
                both from IIT Mumbai. Much of the work on the new software has 
                been done by 50 odd software developers and mathematicians in 
                India. The company has offices in Seattle, Santa Clara, Mumbai 
                and Delhi.<br>
                <br>
                The Webaroo software features “web packs” on a variety of 
                subjects such as news, sports and major global cities.</font></p>
