                return to roots <br>
                </b><br>
                <img border="1" src="worldscan_1.jpg" align="left" width="100" height="121">Barbie 
                is at war. The iconic blonde doll has dumped one Hollywood 
                studio in favour of another that promises more exposure in her 
                ongoing battle to beat back the Bratz — bigheaded, pouty-lipped 
                dolls with street-wise fashion sense. The doll wars have been 
                costly for Barbie’s maker, Mattel, which in January posted a 
                lower quarterly profit partly due to an 11 per cent decline in 
                worldwide sales of Barbie dolls as little girls embraced MGA 
                Entertainment’s Bratz. <br>
                <br>
                Mattel recently dropped Lionsgate as the distributor of its 
                lucrative Barbie DVD franchise and hired Universal Studios to 
                give the directto-DVD films the red-carpet treatment, complete 
                with film trailers, ad campaigns and retail tie-ins.<br>
                <br>
                The two companies also discussed the possibility of a Barbie tie 
                in with Universal’s theme parks and theatrical premieres for 
                upcoming Barbie animated films.<br>
                <br>
                With her impossible proportions and ever-changing careers, 
                Barbie stood by her message that girls can be anything they want 
                throughout most of her 47-year life span. But as the No. 1 
                selling US fashion doll lost young fans to the savvy, 
                multi-ethnic Bratz dolls, Mattel started to chase the teen 
                culture and lose sight of Barbie’s message, independent toy 
                analyst Chris Byrne said. <br>
                <br>
                Mattel’s hook-up with Universal to promote the animated films 
                appeared calculated to bring more visibility to both doll brands 
                and to reposition Barbie with her core fans, Byrne said. “It’s 
                really a strong move for them,” Byrne said. “They really are 
                moving themselves back into owning a specific imaginative 
                space...a Barbie girl is this fantasy princess story line and 
                they’re saying, ‘So why don’t we own that?’” <br>
                <br>
                Richard Dickson, senior vice-president of marketing, media and 
                entertainment for Mattel Brands, said the decision to switch 
                distributors stemmed from the company’s desire to “take a longer 
                term approach” to marketing the Barbie DVDs.</font></p>
