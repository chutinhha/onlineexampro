                <b>Hundreds lose jobs in DP World port takeover<br>
                </b><br>
                <img border="1" src="worldscan_2.jpg" align="right" width="121" height="112">Nearly 
                800 employees of Mina Zayed, including 270 UAE nationals, have 
                been fired following a management takeover by Dubai Ports World, 
                according to recent media reports.<br>
                DP World, the world’s third largest port operator, took over the 
                management of Mina Zayed on April 1, its second major 
                acquisition in the UAE following the takeover of Fujairah port 
                last year.<br>
                <br>
                “On April 12 approximately 800 employees received their 
                termination letters,” a top Mina Zayed official, who was moved 
                out said, as per media reports.<br>
                <br>
                “Many senior people have been replaced including the 
                under-secretary and assistant under-secretary and some have 
                resigned from their posts,” the ex-official was quoted to have 
                said.<br>
                <br>
                Ahmad Al Calily, managing director of Abu Dhabi Ports Company, 
                the new entity created to oversee development of the emirate’s 
                port infrastructure, confirmed, as per media reports, that 270 
                nationals and about 300 expatriate workers will no longer be 
                employed at Mina Zayed under a restructuring plan. But he 
                insisted that all UAE nationals and some expatriates would be 
                absorbed in other government jobs.<br>
                <br>
                Al Calily said the move followed an evaluation of personnel by a 
                committee comprising members of the old and new management, 
                including DP World consultants. <br>
                <br>
                But DP World distanced itself from the job cuts. “We are only 
                responsible for managing the port. It is not a concession 
                agreement,” DP World’s deputy managing director Anwar Wajdi was 
                quoted to have said.</font></p>
