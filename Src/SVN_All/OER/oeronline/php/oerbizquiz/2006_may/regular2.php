    <p align="justify" style="margin: 10px"><font face="Verdana" size="1"><b>A 
    Quick Tip</b><br>
    Gather all your printed sales material, including letterheads and business 
    cards, stand back and examine them as though you’ve never seen them before. 
    Is the design good? Do they tell a great story and make you feel proud of 
    your organisation’s presentation? Would you be impressed if someone else 
    sent this material to you? Consider everything else with your firm’s name on 
    it, including signage vehicles, website, yellow pages advertisement etc. Are 
    they consistent, clean, bright and well produced?<br>
    <br>
    At the end of the day, too many people think that branding asks the question 
    “What is it?”, when the question really should be “What is it for?” </font>
