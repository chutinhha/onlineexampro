<p align="justify" style="margin-right: 10px"><b>
<font face="Verdana" color="#FF0000" style="font-size: 14pt">
<br>
</font></b><font face="Verdana" style="font-size: 14pt"><b>‘What is it’ versus 
‘what’s it for’</b></font><font face="Verdana" size="1"><b><br>
Brand-building has to transcend the rational because it’s a head-and-heart 
thing, says Matt Harrison</b><br>
<br>
Let’s get something straight. The two greatest assets your company has are its 
reputation and brand recognition. If you want to establish greater credibility, 
build perceptions, engender name recognition and break down barriers to trust. 
You have to create valuable publicity to deliver your message to the people 
whose opinion matters through the most credible format possible. <br>
<br>
Since tests of credibility for different forms of marketing promotion 
consistently show that more people regard media coverage as more truthful than 
advertising or mail shots — either via post or e-mail — effective PR/corporate 
communications must be part of your marketing mix. Indeed, in today’s 
increasingly cluttered markets, Oman-based businesses need to find ways to 
differentiate themselves and appeal to their target audience in new ways. And 
that means building a brand, right?<br>
<b>
<br>
Treating customers with respect</b><br>
Gone are the days when companies could patronise their consumers. People these 
days — particularly the youth — are more creative, more selective, and more 
design-conscious. They feel more empowered. They won’t put up with second-rate 
products or services. People are also deluged with information and opportunity. 
The brands they select and build relationships with are those who treat them as 
intelligent adult partners. How many firms do this here? If you want these 
people as your customers, your branding strategy must acknowledge this.<br>
<b><br>
Customer-focused brand</b><br>
People need to feel that organisations are providing them with products that 
deliver real benefits and with the services that help make their lives easier. 
But they won’t be your creative department when it comes to working out exactly 
what these things are. That is your responsibility. Remember, no one begged for 
pre-paid telephone cards, or premium adult ice cream. But they sure picked up on 
these things when they were dreamed up and offered to them. A customer-focused 
brand should pre-empt customer desire for these products through knowledge of 
their customers.<br>
<br>
<b>Slogans</b><br>
When trying to build a brand, people often get obsessed with creating strap 
lines and coming up with jazzy phrases. Nice if you stumble upon a great one, 
like “Just do it”. But they are not necessary to build a great brand. Don’t 
believe me? Then, what are the “strap lines” for brands like: Microsoft, 
Mercedes, Vodafone, Virgin Atlantic, Persil and McDonald’s? See what I mean?<br>
<b><br>
Brand’s and People</b><br>
Today, people are very busy. They don’t respond well to brands, which have lost 
that human perspective. However, they do respond well to a brand that clearly 
“knows its place” in people’s lives — a brand that spells accessibility, 
flexibility, humanity and often a sense of humour. Allow the customer to have a 
major say in how and when s/he drives the brand relationship. <br>
<b><br>
ESPs versus USPs </b><br>
It’s difficult to create a genuine unique selling point these days. It’s even 
more difficult to keep it unique. If it’s a good one, the competition will 
follow. There’s a further issue — own label brands are often stronger than 
branded brands. I remember reading about a UK-based marketing manager at Shell. 
He was asked: “Who’s your key competition?” “Easy,” he said. “Esso, BP and 
Texaco. Perhaps to a lesser extent Jet and Elf.” When asked where Tesco stood in 
all this, he shrugged his shoulders. You can build genuine, enduring 
differentiation through an ESP (emotional selling proposition). That’s 
essentially about a distinctive and attractive personality, with which people 
feel they can engage. Customers want to know: “Do I like these people?”<br>
<b><br>
Talk to Your Staff</b><br>
Let’s talk about a simple point that’s often overlooked — especially in service 
companies. Real brand reputation is created at the point of delivery — the guy 
at the check-out, the reception and the petrol pump. If your own people do not 
know the strategy and agenda and understand the central role they play in 
building your brand, it won’t be delivered. So have clear internal processes, 
which inform and involve front-line staff.<br>
<b><br>
It’s What You Do...</b><br>
It’s more than just having motivated people at the point of delivery. It’s also 
about demonstrating the brand’s commitment — launching new products that make 
the point. For example, BA’s flat-beds on long-haul business class, and Marks &amp; 
Spencer’s returns policy. These can reach beyond the traditional provision of 
better products and services. “Good citizen” sponsorships which are clearly of 
value to the communities they serve can be very strong in building a customer 
conscious brand. <br>
<b><br>
<img border="0" src="marketing.jpg" width="150" height="108" align="right" hspace="5" vspace="2">Look &amp; 
Feel</b><br>
Strong brand building needs cohesive communications and delivery. Yet, the most 
likely breakdown of the alchemy that represents a powerful brand is 
inconsistency of approach. Not only mismatch between rhetoric and reality, but 
also different messages, with different tones, personality and core values 
disseminated and therefore consumed through different channels. There are now 
plenty of those channels. Online and offline, paid and unpaid media, broadcast 
and narrowcast... the list goes on.<br>
<b><br>
Rocket Science</b><br>
Brand-building has to transcend the rational. Put in simple terms, it’s a 
head-and-heart thing. If there were a logical equation, everyone would be brand 
leader. However, it’s not rocket science — it’s alchemy. Resist the temptation 
to chop and change. Evolve, progress, innovate, grow — but always remember to 
nurture the brand.</font></p>
