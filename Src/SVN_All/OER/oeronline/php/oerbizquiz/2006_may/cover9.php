            <br>
            CONSOLIDATION 
            mode</b></font><font face="Verdana" size="1"><b><br>
            Though NBO has slipped on the OER Top 20 rankings by two positions, 
            the bank has done quite well after the management restructuring and 
            private placement</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="nbo1.jpg" width="180" height="219" align="left" hspace="5" vspace="2">W</b></font><font face="Verdana" size="1">hen 
            OER met National Bank of Oman’s CEO Andrew Duff last year, days 
            after he took charge of the bank, the latter had talked about the 
            bank already being on course to the recovery path. He also talked 
            about the mandate to consolidate the recent gains. Less than a year 
            since then, he has almost managed to achieve his stated goals.<br>
            <br>
            2005 can easily be considered a watershed year for the bank. During 
            the second half of 2005, NBO finalised its strategic alliance with 
            The Commercial Bank of Qatar (CBQ), which included purchase of 34.9 
            per cent of the bank’s shares by CBQ. This first-ever GCC 
            cross-border strategic alliance between the two banks has resulted 
            in NBO’s net worth shooting up to RO 167.9 million during the year, 
            thanks to the private placement of 10 million shares to CBQ in July 
            2005 at a price of RO 4.450 per share as well as the profit recorded 
            for the year. The market capitalization has increased significantly 
            from RO 171 million as on 31 December 2004 to RO 365 million as on 
            31 December 2005.<br>
            <b><br>
            Record net profits</b><br>
            NBO’s net profit for 2005 was RO 20.3 million. This is a record for 
            the bank and reflects sound, broad-based operating growth, 
            continuously reducing levels of provisions and good momentum in 
            recovering from prior years’ delinquent customers. In corporate 
            banking, progress was achieved on most fronts viz. operating 
            profits, asset growth, higher fee earnings and costs. Operating 
            profit was 46 per cent over the 2004 level, advances 32 per cent 
            over the 2004 level and deposits 58 per cent over 2004 level. Duff 
            says: “A number of initiatives were taken to improve the confidence 
            of the customer in the market place and this has resulted in 
            identifying 29 new clients as well as substantially increasing the 
            share of business from existing clients. Co-financing opportunities 
            and other synergies with the strategic alliance partner, CBQ, are 
            being pursued.”<br>
            <br>
            In consumer banking, the business was slow in the first half of 2005 
            due to uncertainties on account of the proposed merger with 
            BankMuscat and subsequent developments. However, this was 
            compensated by positive growth in the second half of 2005. <br>
            <br>
            The distribution-related initiatives included opening of a new 
            branch in Suwaiq, relocating Barka and Khaburah branches to better 
            premises, renovation of Salalah branch, and installing a new ATM in 
            Al Khoud. The product and services initiatives included re-launch of 
            the savings prize scheme as Al Kanz, amendments to the SRL Policy, 
            simplification of the approval process, FIFA card deal with 
            MasterCard exclusively for NBO in Oman and finalisation of Credit 
            cards “Super Value Offers” booklet, with discounts from leading 
            retail outlets in Oman.<br>
            <br>
            The investment banking’s core asset management activities witnessed 
            a 42 per cent growth in assets under management and outperformed the 
            index. On the overseas business, Duff says, “Plans to expand the 
            proprietary book, both in Oman and abroad, are underway. Our stated 
            strategy for our five branches in Egypt continues to be on a ‘care 
            and maintain’. The focus will continue to be on a conservative 
            business model and recovering non-performing loans. Egypt is not 
            envisaged to be a drag on earnings. The UAE operations have shown 
            improved profitability during the year and it will be our focus of 
            investment and resources with the intention of optimising the 
            potential of the licence we hold to operate in the UAE.”<br>
            <b><br>
            Remedial management</b><br>
            The remedial division made a significant progress with record 
            collections totaling RO 34.4 million (27.3 million in 2004), 26 per 
            cent above the previous year. There has been significant reduction 
            in the non-performing loans, coming down from RO 256 million in 2004 
            to RO 111 million in 2005. Duff explains: “This has been the result 
            of implementation of multiple strategies and well executed action 
            plans. The ratio of NPLs to gross loans dropped to 15 per cent in 
            2005 from 31 per cent in the previous year. We expect this to 
            further drop in 2006 to below 8 per cent.”<br>
            <br>
            <b>Outlook</b><br>
            On the future outlook, Duff says, “We will continue to work towards 
            further strengthening our position in Oman by pursuing products, 
            sales and customer service leadership and excellence. We will also 
            build our franchise in the UAE. The focus will be on revenue growth 
            and diversification, coupled with strong cost discipline aimed at 
            reducing our cost to income ratio and improving profit from core 
            operations. We will work closely with our strategic alliance 
            partners, CBQ, to capitalise on regional business opportunities and 
            transfer best practices across both organisations.”</font></p>
