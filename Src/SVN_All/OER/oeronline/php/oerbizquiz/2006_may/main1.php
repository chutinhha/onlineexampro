<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" style="font-size: 14pt"><b>Oman, a special market for 
Cisco</b></font><font face="Verdana" size="1"><br>
<b>Cisco 
Systems has identified the Gulf region as one of the most important emerging 
markets in its growth strategy. Akshay Bhatnagar talks to Cisco Systems’ Eyad M. 
Alqadi, Regional Sales Manager, Gulf Region Public Sector &amp; Enterprise, to find 
out more about it<br>
<br>
<img border="0" src="facetface.jpg" width="250" height="161" align="left" hspace="5" vspace="2">What brings you to Oman?</b><br>
Cisco has been associated with Oman for quite some time. We now consider Oman as 
an important market for business expansion in the Gulf region. We are in the 
process of opening our full-fledged local office in the next 12-18 months.<br>
<br>
As part of our commitment to the Sultanate, we organised our regional meeting in 
Oman. Over 120 Cisco Systems’ employees from the Middle East region participated 
in the meet, which we call ‘All Hands Team Meeting,’ with inbuilt multiple 
workshops focusing on developing our market knowledge and functional skills. As 
part of the meet, we also pay back to the community. This time, we painted a 
school and set up a computer lab there. <br>
<b><br>
Is the decision to set-up your full-fledged local operation driven by the 
changing dynamics of the ICT sector in Oman?</b><br>
Absolutely! Five years back, the IT applications were mostly limited to large 
companies. But today technology has made it possible for companies (irrespective 
of their scale of operations) and individuals to seamlessly connect to each 
other. We have seen a healthy increase in ICT (information, communications &amp; 
technology) spending in Oman. Earlier, technology was costly. Today, it is an 
enabler. <br>
<br>
The ICT growth in Oman is mainly driven by the government’s support and 
programmes to spread IT literacy and Internet in the Sultanate. The government’s 
initiative on the e-governance front, to connect all ministries and government 
offices with each other on a single common platform, is a commendable effort. 
The government was supportive of IT 10 years back also but it now understands 
better that in order to compete in the global market, the Sultanate needs to 
hike its ICT spending and improve the skills of its human resources.<br>
<b><br>
Do you think that ICT is growing slower than the desired rate?</b><br>
Change is always welcome, but at the same time, depending upon the environment, 
change needs time to take place. We cannot expect Oman to change overnight. From 
our experience here, we find that lot of positive developments are taking place, 
which is an encouraging sign. We believe there is a clear direction from the 
government that there is a direct correlation between the ICT spending and the 
economic development. Many countries have spent quality time to ensure that the 
technology initiatives, when rolled out, help in the growth of every strata of 
the population.<b><br>
<br>
What is Cisco’s plan to tap the burgeoning telecom sector and infrastructure 
development in Oman?</b><br>
We are driving a number of initiatives in close partnership with the government 
to bring the people of Oman closer to the information technology. These 
initiatives are not meant for business generation. They are part of our 
corporate social responsibility endeavour, directed towards transforming the 
education system, spread of e-governance and expansion of the telecom industry 
in the Sultanate.<br>
<b><br>
Could you pinpoint any specific programmes you are introducing in Oman?</b><br>
Whichever market Cisco enters, it invests in introducing the best systems and 
practices to serve the unique needs of the citizens of that country. In Oman, 
based on our analyses and market feedback, we found that the development of 
human capital development is the key area on which we should work. Education is 
also an important factor linked to it. <br>
<br>
At Cisco, we have developed a number of programmes for the development of human 
capital. One such programme is Cisco’s Networking Academy Program (CNAP). It is 
a comprehensive e-learning programme that provides students with the Internet 
technology skills essential in a global economy. The Networking Academy delivers 
web-based content, online assessment, student performance tracking, hands-on 
labs, instructor training and support, and preparation for industry standard 
certifications. We are going for a local ownership here. We are working with the 
Ministry of Education on this and a number of similar projects. We are sharing 
our knowledge based on our experience on the practices in the educational 
institutions in the more advanced markets.<br>
<b><br>
How are Cisco’s products and services driving the business segment — large 
enterprises as well as small to medium businesses — in Oman?</b><br>
To support businesses and organizations in Oman, we recently launched the Cisco 
Unified Communications system, a new suite of voice, data and video products 
designed to help enterprises and mid market businesses communicate more 
effectively and improve productivity. Cisco Unified Communications system allows 
employees to exchange information, whether by email, voice or fax, via a wide 
choice of fixed and mobile communication devices.<br>
<br>
The system enables organizations to seamlessly integrate their communications 
systems with their IT infrastructure. To ensure businesses can benefit from 
this, Cisco is helping companies evolve their technology through a clear path, 
or Smart Business Roadmap, allowing them to match solutions with their immediate 
and long-term business needs, and invest for the future without the associated 
cost or risk.<br>
<br>
To tackle small and medium businesses (SMBs) technology concerns, we have 
introduced the Business Communications Solution, the first smart, simple, 
secure, and integrated solution of communications products and services, 
purpose-built and tailored for small to medium-sized business. The solution is 
designed to help SMBs in Oman control costs, improve operations and gain a 
sustainable competitive advantage. The solution allows SMBs to quickly and 
easily configure their networks and enable advanced technologies such as 
wireless LANs and IP Communications.<br>
<br>
Additionally, Cisco Systems has been a key participant at the local Information 
and Communications Technologies (ICT) exhibitions over the last few years, 
showcasing the latest technologies and business solutions across all sectors.
<br>
<b><br>
Do you offer any tailor-made solutions for your clientele in Oman?</b><br>
We have various products for each vertical and they have been introduced in this 
market on the basis of the market feedback and requirement studies. We find a 
great fit between what Cisco is offering and what the customers are demanding 
and looking for in Oman.<b><br>
<br>
How do you rate the GCC countries as early adopters of new technologies despite 
the high cost factor?</b><br>
The GCC countries are definitely the frontrunners in implementing latest 
technologies. What’s interesting is that the GCC countries are not only adopting 
new technologies, but in many cases, they are technologically more advanced 
compared to many of the so-called advanced markets. There is lot of legacy 
technology in the western markets. <br>
<br>
The Gulf countries realise fully well the importance of ICT in transforming 
their economic fortunes. And this is what is driving the trend. Interestingly, 
in many of the Gulf countries, they have created an ICT authority headed by a 
minister to drive ICT in the country. The Gulf nations are deploying some of the 
highly advanced technologies, which the West is still only mulling over. Many of 
the clients we are working with have no qualms in stepping forward to use the 
new technologies, as they understand the need to change and adapt fast with the 
changing times. <br>
<br>
The Gulf economies are growing at a faster rate and committing huge investments 
in infrastructure development sectors. Take for example Oman, where gigantic 
projects like The Blue City and The Wave are coming up. To make a comparison, 
the European countries are not even mulling over projects of such scale. That’s 
what makes Oman such a special market for us.</font></p>

