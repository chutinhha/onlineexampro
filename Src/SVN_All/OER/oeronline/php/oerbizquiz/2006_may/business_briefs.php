                        <b>Gulf Air announces more flights from Muscat<br>
                        </b><br>
                        </font><font face="Verdana" size="4">G</font><font face="Verdana" size="1">ulf 
                        Air has announced as many as 69 additional flights from 
                        its Muscat hub to popular destinations in its network, 
                        beginning this summer. This includes introduction of new 
                        services to Paris, Amman, Dammam, Kathmandu, Sana’a, 
                        Islamabad, and Jakarta and, increasing its services to 
                        some of its existing destinations. This makes a total of 
                        195 flights per week flying out of Muscat from its 
                        current schedule of 126 flights per week. <br>
                        <br>
                        The summer schedule will see Gulf Air flying non-stop 
                        seven times a week to Jakarta and Kathmandu, six flights 
                        to Amman, four flights a week to Paris, two services to 
                        Islamabad and Dammam, and a weekly flight to Sana’a. A 
                        non-stop four times a week service to Kuala Lumpur has 
                        also been introduced.<br>
                        <br>
                        The summer schedule also offers more flights to some of 
                        its existing destinations. For instance, the daily 
                        flights to London have been increased to double daily 
                        (except Thursdays) non-stop flights making it a total of 
                        13 flights a week.</font></p>
