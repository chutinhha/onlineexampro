                        <b>NBO net profit up<br>
                        <br>
                        </b></font><font face="Verdana" size="4">N</font><font face="Verdana" size="1">ational 
                        Bank of Oman announced the results of its operations for 
                        the first three months of 2006. In line with its growth 
                        strategy, the Bank’s net profit showed an increase of 83 
                        per cent for the period ended March 2006 as against the 
                        same period in 2005, reflecting a significantly higher 
                        profit of RO 5.5 million for the period.<br>
                        <br>
                        The operations of the Bank have shown growth in all 
                        areas with net loans growing by 18 per cent compared to 
                        March 2005 and deposits growing by 16 per cent over 
                        March 2005 resulting in a significant 16 per cent 
                        increase in Net Interest Margin for the period up to 
                        March 2006 of RO 8.4 million. Andrew Duff, Chief 
                        Executive Officer, indicated that the increase in Net 
                        Margin reflected the positive trend in the last two 
                        quarters.</font></p>
