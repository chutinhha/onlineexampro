                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Renaissance Chairman meets SOCAR President<br>
                        </b><br>
                        <img border="1" src="biz_1.jpg" align="right" width="200" height="135"></font><font face="Verdana" size="4">R</font><font face="Verdana" size="1">enaissance 
                        Services SAOG Chairman Samir Fancy led a delegation of 
                        Board Directors and Senior Management to the Caspian 
                        region to visit the operations of BUE Marine, the 
                        recently acquired subsidiary of Renaissance Services. 
                        BUE Marine is the leading marine operator in the 
                        Caspian. The tour provided an opportunity for the 
                        company’s representatives to meet with key stakeholders, 
                        as well as explore growth and investment opportunities 
                        for all Renaissance businesses.<br>
                        <br>
                        The visit included a meeting in Baku with SOCAR (State 
                        Oil Company Azerbaijan Republic) President, Rovnag 
                        Abdullayev, who spoke highly of the “excellent 
                        relationship” between SOCAR and BUE Marine. He welcomed 
                        the Renaissance visitors and said, “the high level 
                        delegation speaks of the level of Renaissance’s 
                        commitment to Azerbaijan,” and that “SOCAR looks forward 
                        to working closely with Renaissance in the years ahead”. 
                        Samir Fancy affirmed the importance of the relationship 
                        with SOCAR and briefed the SOCAR President on 
                        Renaissance plans for investment and expansion in the 
                        region. <br>
                        <br>
                        Renaissance is one of the largest public listed services 
                        companies in the region. A large proportion of 
                        Renaissance business is engaged in providing quality 
                        services to the oil and gas sector, where it is a 
                        leading turnkey contract services provider; and has one 
                        of the top 10 marine support fleets in the world with a 
                        significant presence in the Gulf, the Caspian region and 
                        S.E. Asia. Renaissance is recognised as an Omani company 
                        with substantial international credentials, successfully 
                        exporting its services abroad.<br>
                        <br>
                        “Our established presence in the Caspian Sea basin 
                        offers Renaissance tremendous scope for growth,” said 
                        Fancy. “We have excellent relationships with all our 
                        stakeholders and a strong reputation and record for 
                        safety, quality and delivery.”<br>
                        <br>
                        The company’s 2005 Annual Report states that 75 per cent 
                        of the annual revenue of RO 106.4 million was generated 
                        outside Oman and 60 per cent of revenue is generated in 
                        services to the oil &amp; gas industry.</font></p>
