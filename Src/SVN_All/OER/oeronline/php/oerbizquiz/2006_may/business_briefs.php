                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>BankMuscat net profit up<br>
                        </b><br>
                        </font><font face="Verdana" size="1">
                        <img border="1" src="biz_3.jpg" align="left" width="200" height="115"></font><font face="Verdana" size="4">B</font><font face="Verdana" size="1">ankMuscat 
                        has achieved a net profit of RO 13.25 million during the 
                        first quarter of the year 2006, as against a net profit 
                        of RO 10.34 million reported in the first quarter of 
                        2005 — an impressive growth of 28 per cent. <br>
                        <br>
                        Meanwhile, BankMuscat’ Brokerage Unit has once again 
                        received the ‘Best Brokerage Award’ (by trading volume) 
                        at a function organised by the Muscat Securities Market. 
                        In addition, BankMuscat Brokerage Unit also bagged the 
                        second position in terms of ‘Services Offered by a 
                        Brokerage House’ in the Sultanate for the year 2005.<br>
                        <br>
                        In another development, BankMuscat recently hosted an 
                        evening branded “Partners-in-Progress” to thank and 
                        recognise its Card Service department’s merchant 
                        relationships. The Bank also announced the first 
                        ‘Partners-in-Progress Best Merchant 2005’ awards during 
                        the evening to recognise its top merchants by way of 
                        contribution to business volume across various merchant 
                        categories.<br>
                        <br>
                        In another positive development for the bank, Bahrain 
                        based BankMuscat International (BMI) has announced the 
                        successful placement of a US$ 35 million-subordinated 
                        debt to international investors, with the lead managed 
                        by BankMuscat, Oman. The issue was almost three times 
                        oversubscribed.<br>
                        <br>
                        The Muscat fund, managed by the Asset Management 
                        Division of BankMuscat, recently held its Annual 
                        Ordinary General Meeting to deliberate and seek approval 
                        on the Chairman’s statement for the year ended 31 
                        December 2005; the managing company’s report; auditor’s 
                        report and audited balance sheet and statement of 
                        operations and also consider a final dividend of 100 
                        baisa per unit and a bonus of 20 units per 100 
                        registered as at the date of the AGM for the financial 
                        year 2005. <br>
                        <br>
                        Shareholders were also asked to consider the related 
                        party transactions for the year and consider the terms 
                        for the appointment of auditors for the year 2006. The 
                        successful implementation of the prudent recommendations 
                        made by the Investment Manager resulted in a 46.4 per 
                        cent rise in the NAV of the fund during 2005. The Fund 
                        recorded net profits of RO 1.69 million for the year 
                        ended 31 December 2005, up by 61.6 percent as compared 
                        to last year.</font></p>
