            <br>
            ENERGIZING GROWTH</b></font><font face="Verdana" size="1"><b><br>
            Going into 2006, Renaissance has a stronger balance sheet than ever 
            before and a wider geographical presence as well<br>
            </b><br>
            </font><font face="Verdana" style="font-size: 11pt"><b>R</b></font><font face="Verdana" size="1">enaissance 
            Services SAOG has moved up the ladder to Number 
            4 in
            the OER Top 20. It was placed 
            at Number 6 last time. And to do that, it has chartered the 
            inorganic growth route: mergers and acquisitions. But this burst of 
            inorganic growth comes on the back of significant organic growth as 
            well. <br>
            <br>
            <img border="0" src="renais.jpg" width="200" height="213" align="left" hspace="5" vspace="2">The 
            ascension of this giant in diverse service fields can be gauged from 
            the fact that it posted the highest growth — 70 per cent — among the 
            Top 10 companies in the financial year 2005. No small achievement 
            this. Renaissance has moved ahead on the growth track consistently. 
            It clocked a growth of 36 per cent in 2004 and 58 per cent in 2003. 
            This clearly explains why it keeps inching up on the OER Top 20 
            rankings, year after year, in spite of major new listings joining 
            the market as well. The acquisition of Topaz Energy &amp; Marine SAOG, 
            through a 100 per cent share swap and the subsequent acquisition of 
            100 per cent shareholding in BUE Marine Limited, has significantly 
            strengthened the company’s presence in the oil and gas industry. The 
            company’s Chairman, Samir Fancy, describes the developments as 
            “strategic in the making and timely in its execution”. <br>
            <b><br>
            Financial Performance </b><br>
            With revenues over RO 106.6 million, the company leads the services 
            sector with itsoperations spread over to diversified businesses that 
            include marine services, engineering services, contract services, 
            information &amp; healthcare technology; education and training and 
            media communications. <br>
            <br>
            In its tenth year now, Renaissance has ushered in a transformational 
            change that has “altered the scope, scale and dimension of the 
            company and set it on a future course of sustainable and profitable 
            growth”. While the total revenue has leapfrogged from RO 62.580 
            million to RO 106.5 million between 2004 and 2005, fixed assets have 
            shot through the roof - from RO 18.1 million in 2004 to RO101.7 
            million in 2005. Market capitalization has zoomed from RO 39 million 
            in 2004 to RO 118.6 million in 2005. According to Fancy, 
            three-fourths of Renaissance’s total revenue had come from outside 
            Oman and two-thirds from the service of the oil and gas industry 
            group. “Importantly,” succinctly points out Fancy, “we expect the 
            latter revenue stream to grow materially in the year ahead.” Put it 
            differently, the message Fancy gives to the market is that the group 
            is focused on long-term growth by its timely increased interest in 
            the hottest sector — oil and gas. During 2005, marine and 
            engineering businesses posted a revenue of RO 51.559 million and 
            netted a profit of RO 5.682 million; contract services brought in RO 
            35.896 million revenue and RO10.561 million profts; technology 
            business did business worth RO10.663 million and posted a profit of 
            RO1.090 million. While the technology business has enjoyed 
            particular success in its healthcare and financial sector 
            initiatives, the company’s contract services has achieved a net 
            increase in its market share and sustained its competitive position 
            abroad. The media business has posted its best results till date and 
            the education &amp; training business has fared well in a tough market 
            environment.<br>
            <br>
            Analysts Fincorp point out that while overall profit has increased 
            slightly, profit margins have declined in spite of the substantial 
            growth in revenue. Stephen R. Thomas, the company’s CEO, sums it up: 
            “This has been a year of great activity and success for Renaissance. 
            We have increased stability of long-term sustainable earnings and 
            reduced reliance on highly profitable but highly volatile short-term 
            earnings. We have delivered a strengthened balance sheet, with more 
            than 5.5x increase in fixed assets, more than 2.5x increase in net 
            worth, and a tripling of the company’s share capital. The 
            shareholder’s value has gone up significantly, with a near tripling 
            of the company’s market capitalization.” <br>
            <b><br>
            Best Practices</b><br>
            Renaissance claims that the true test of a company’s integrity and 
            purpose are its business performance and the transparency of its 
            business practices. “It is important to look beyond the books and 
            into the company’s commercial relationships and the way in which its 
            senior managers behave,” points out Thomas. “We believe in 
            conducting businesses with integrity. Renaissance wins business by 
            bringing value, quality and capability to the market and exceeding 
            customer expectation at extremely competitive prices,” asserts 
            Thomas. In 2005, Renaissance paid two stock dividends of 10 and 62.5 
            per cent. With the increase in the share capital following Topaz 
            acquisition, a 25 per cent cash dividend was paid. Following the 
            current corporate trend, Renaissance is also going for a 10:1 stock 
            split to widen investor base and improve liquidity.<br>
            <b><br>
            Challenges &amp; Forecast</b><br>
            Renaissance Board and Management are reviewing the strategic 
            thinking in all businesses. Says Fancy: “We are confident that all 
            our businesses have a bright future ahead…The highly cash-generative 
            services business also complements the asset-based businesses, which 
            offer sound long-term investment opportunities around established 
            and proven core competencies.” <br>
            <br>
            The macro economic environment in the Middle East and the Caspian 
            Region and other key markets, where Renaissance operates, is 
            predominantly “positive and bullish”. The company is looking at 
            growth opportunities at two levels: within each of the operating 
            groups, there is a growth focus comprising a choice of organic, 
            merger-and-acquisition, and geographic expansion opportunities. 
            Having achieved the primary growth focus on size and scalability, 
            Renaissance is confident of consolidating the current phase, and 
            focus on “maximizing delivery of the growth strategies of its prime 
            assets”. Adds Thomas: “Going into 2006, Renaissance has a stronger 
            balance sheet than ever before; a wider geographical presence; and 
            an increasingly blue chip client base wherein opportunities abound. 
            We have the right business model, the right people and we have 
            belief. We look forward to the challenges ahead.” Not to be taken 
            lightly, of course!</font></p>
