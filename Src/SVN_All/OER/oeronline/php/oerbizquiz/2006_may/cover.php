            2005, overall, was also extremely positive. The profits for the year 
            2005 increased by 22 per cent to RO 263 million from the figure of 
            RO 215 million last year. The total market cap of the OER Top 20 on 
            31 December 2005 was RO 3.642 million, up by 22 per cent compared to 
            2004. To arrive at this figure, a notional price of Omantel shares 
