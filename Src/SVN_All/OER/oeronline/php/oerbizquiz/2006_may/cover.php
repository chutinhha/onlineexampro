            <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">There has been a 
            major change in the top five companies in Oman. Omantel comes 
            straight into the number one slot, overthrowing Shell from the 
            number one spot, which they had enjoyed since the inception of the 
            OER Top 20 way back in 1999, to the number two spot. BankMuscat has 
            slipped to the third place to accommodate Omantel, Renaissance 
            (parent company of OER) has stormed into the fourth place from being 
            the number six last year and Al Maha Petroleum has slipped two 
            places to number five. Oman Oil and Oman Aviation have been knocked 
            out of their last year’s number four and five spots respectively.
            <br>
            <br>
            Omantel is now the largest public company in Oman with a growth in 
            revenue of 9.28 per cent compared to last year. The chairman of the 
            company, Saud bin Nasser Al Shaikili, in his report to the 
            shareholders, explains that 2005 was one of the most memorable years 
            for the company, one in which they had to face two highly 
            challenging events. Firstly, the sector liberalization, where the 
            company saw intensive competition in the mobile sector, and second, 
            the privatization of Omantel, the largest Public Offering in the 
            Sultanate. <br>
            <br>
            According to Al Shaikili, the other remarkable achievement for the 
            year was Oman Mobile crossing the 1 million mark in terms of mobile 
            subscriptions, despite intensive competition. He points out that the 
            total Group revenue rose by 9.4 per cent to RO 270.6 million. The 
            operating expenses increased by 15.4 per cent to RO 193.8 million. 
            Al Shaikili explains that the increase in operating expenditure was 
            due to increase in international administration expenses, 
            depreciation, marketing and operating &amp; maintenance expenses. He 
            adds that the increase in operating expenses is to enhance the 
            network coverage and to comply with the quality of service 
            parameters that are required in a competitive scenario. <br>
            <br>
            According to Al Shaikili, the Company has delivered a Net Profit of 
            RO 67.8 million for the year 2005. The Board of Directors recommend 
            a dividend of RO 52.5 million — i.e. 070 baizas per share, which is 
            equivalent to 77.4 per cent of the Net Profit delivered by the 
            business for the full year 2005. <br>
            <br>
            This level of dividend will generate a dividend yield of 4.17 per 
            cent on the share price as at 31 December 2005. Al Shaikili explains 
            to the shareholders that during the year 2006, the sector 
            liberalization is likely to intensify. He adds that the company has 
            been focusing on enhancing its internal competencies in terms of its 
            human resources, processes and systems during the last two years, 
            and has hiked investment in infrastructure and increased investment 
            in seamless coverage of mobile network, which are some of the major 
            investments/initiatives to prepare the company to position 
            effectively in the evolving competitive business scenario.</font></p>
