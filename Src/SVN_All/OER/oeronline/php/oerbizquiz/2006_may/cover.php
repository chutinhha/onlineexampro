            <font face="Verdana" style="font-size: 10pt"><b>F</b></font><font face="Verdana" size="1">or 
            the third consecutive year, Oman’s economy continued to do well, and 
            the Muscat Securities Market (MSM), in particular, continued to 
            remain significantly bullish in 2005. The price of oil continued to 
            increase during 2005. Oman crude realised an average price of 
            US$48.73 per barrel in 2005 compared with the budgeted price of 
            US$23 per barrel. This resulted in a reversal of the planned 
            budgeted deficit to a surplus. The year 2005 closed with a budgetary 
            surplus of RO 1.479 billion as against a projected deficit of RO 540 
            million.<br>
            <br>
            The MSM also continued to perform well during the year 2005. Cash 
            liquidity during the year continued to remain easy and liquidity of 
            stocks continued to improve. The new IPO of Omantel, offering 30 per 
            cent of its shares to the public in mid 2005, was a significant 
            event in the issue market. Trading volumes amounted to RO 1,407 
            million in 2005 compared to RO 759 million in 2004, a substantial 
            increase of 85 per cent. <br>
            <br>
            The benchmark MSM 30 index ended the year at 4,875.11 points, a gain 
            of 44.4 per cent for the year. The top performer was the banking and 
            investment sector, which was up 51.5 per cent, followed by the 
            industry sector, which gained 34.4 per cent. The services sector 
            ended with an increase of 29.9 per cent for the year. Foreign 
            participation in the capital of the listed joint stock companies 
            decreased during the year to 16.2 per cent compared to 18.2 per cent 
            last year.</font></p>
