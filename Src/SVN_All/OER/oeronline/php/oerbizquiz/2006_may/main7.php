<p align="justify" dir="ltr" style="margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>You Cannot Train Employees to Be Ethical</b></font><font face="Verdana" size="1"><br>
<b>David Batstone emphasizes on the five key cultural disciplines that could 
move a company towards more ethical wisdom</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b>C</b></font><font face="Verdana" size="1">ompanies that lack integrity do not 
develop overnight. A series of small, subtle choices led them to their fate; 
each decision set the stage for the next, more corrosive act. Essentially, I 
don’t believe that ethics can be taught. The problem actually lies in the 
company’s culture, and how it encourages individuals to consider deviant 
behavior as standard operating procedure.<br>
<br>
To change a corrupt culture, new disciplines have to be adopted and reinforced. 
It’s vital to recognize that individuals do not need additional information or 
outside training to grow their integrity. The answer already lies within them; 
it just needs nurturing. I had the pleasure of spending some time recently with 
Dr. Jack Gilbert, President of New Page Consulting, a firm focused on 
organizational integrity and ethics.<br>
<br>
Dr. Jack has taken considerable care to study the dynamics of decision-making 
within organizations of all sizes. His research points to five key cultural 
disciplines that move a company toward more ethical wisdom. Because they go 
beyond compliance, codes of ethics, and ethics training, I’d like to name them 
here:<br>
<br>
<b><img border="0" src="manag.jpg" align="left" hspace="5" vspace="2">Mindfulness</b>
<br>
Individuals have an internal sensor that&nbsp; alerts them to wrongful 
decisions. It is a private voice that makes us feel uncomfortable when our 
integrity is at risk. But they often choose to turn that sensor “off” once they 
fall for the “everybody does it” rationalization. Jack argues that companies 
grow integrity once they encourage their employees to put discomfort on the 
table, even if the warning signals are weak.<br>
<br>
<b>Voice</b><br>
This next discipline brings mindfulness into the public conversation. A 2003 
study conducted by the Ethics Research Center uncovered that workers do not 
raise ethical issues on the job for two main reasons: 1) They fear personal 
retaliation; and 2) they are convinced that senior management won’t do anything 
about an ethical problem once it is brought to their attention. In that light, 
“voice” only becomes heard in an organization once its leaders reward employees 
for speaking up. Jack laments the typical response to someone who raises a red 
flag: “So, do you have a better solution?” That should not matter, says Jack. A 
healthy organization can accept authentic self-scrutiny as the first step in 
looking for a better path.<br>
<br>
<b>Relationship</b><br>
Ethical dilemmas are best addressed as colleagues, not as critics. If 
mindfulness is the antithesis of ignorance, Jack suggests that respect is the 
antithesis of being judgmental. When we respect our colleagues and approach each 
other with generosity, we can more easily address tough issues. The key is to 
stay fact-based and avoid blame-based accusations. Ethical breaches often are 
allowed to exist because “they aren’t being done in my backyard.” Respectful 
relationships allow us to move across functional silos in an organization and 
see ourselves as part of the problem as well as the solution.<br>
<br>
<b>Tenacity</b><br>
As I already noted, the erosion of integrity rarely transpires in big, 
black-and-white decisions. The cracks in one’s character in the poor discernment 
between what is the right thing to do and the less right thing to do. Once we 
“get away” with the less right thing, we then are tempted to move on to even 
bolder shades of gray. It takes tenacity to take on difficult conversations — 
where the tools of denial, defensiveness, and even anger are at work — and stay 
undaunted in the quest for ethical action.<br>
<br>
<b>Legacy</b><br>
Leaders can be especially influential in placing a firm’s vision and values 
above results. It is very difficult for, say, a middle manager to always 
prioritize the long-term impact of the operation. But the leader can set a tone 
that prevents the culture from becoming consumed with short-term goals. A firm 
expresses the highest integrity once it weighs the future legacy of the company 
as a vital factor in each decision at hand.<br>
<br>
I cringe whenever I hear this self-defeating belief: “I don’t expect ethical 
behavior at my company because it’s not in the DNA of business.” Not that I 
would expect ethical wisdom to be a genetic pre-disposition. As Jack outlines 
for us, integrity all comes down to practical disciplines that help 
organizations to leverage good sense and collective wisdom.<br>
<br>
<i>David Batstone is the author of Saving the Corporate Soul and teaches 
business ethics at University of San Francisco, United States.</i></font></p>

