<br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>Saab like never before</b></font><font face="Verdana" size="1"><b><br>
Combining the power and capability of a SUV with the sporty driving feel, the 
all-new Saab 9-7X is perhaps the most different kind of Saab. OER probes further 
about the first SUV from Saab in the test drive</b><br>
<br>
Saab and its parent company, General Motors, are working hard to give further 
momentum to the Scandinavian marque’s line-up. The launch of Saab 9-7X, 
developed by using the General Motors GMT360 architecture, is a step in that 
direction. The new AWD wagon targets the lucrative SUV or luxury 4WD market by 
promising driver satisfaction and performance as well as a touch of luxury.<br>
<br>
<img border="0" src="saab.jpg" width="300" height="174" align="right" hspace="5" vspace="2">With 
standard features like on-demand (displacement on demand DOD), and 
all-wheel-drive, the vehicle’s traction and handling performance are optimized. 
And the preloaded-clutch, limited-slip differential at the rear ensures that 
energetic cornering is not compromised by the AWD system. <br>
<br>
To ensure a comfortable ride and maximize handling response, the Saab 9-7X uses 
a uniquely tuned double A-arm front suspension rig with front coil spring. At 
the rear lies a five-link, electronically controlled rear air suspension setup. 
A low ride height indicates that the 9-7X will be more of a black-top cruiser 
than a mudslinger. All shock absorbers are valved to help provide firm body 
control for confident handling, which is very useful on the roads in Oman, 
including the interior regions. <br>
<br>
Moving on stocky P255/55R tyres and 18-inch alloy rims, 9-7X comes with two 
all-aluminium engines in Oman. At the entry-level, a big 4.2-litre straight 
6-cylinder delivering 290 HP at 6,000 rpm and 375Nm of torque is offered. For 
those looking for a much stronger performance with the willingness to pay more, 
there is V8 motor displacing 5.3-litres and generating 300 HP at 5200 rpm and 
447Nm of torque. Saab’s strategy to opt for much larger engines for 9-7X is an 
interesting move. <br>
<br>
The new vehicle has a traditional Saab three-port grille, while flared wheel 
arches and a smooth shoulder line give the SUV a sporty, yet balanced 
appearance. One can’t agree more with Michael Mauer, Saab’s Executive Director 
of design on what he says about 9-7X’s styling: “The exterior and interior 
design of the Saab 9-7X are in line with the vehicle’s dynamic road behaviour 
and conveys the sporty refinement of a Saab, rather than the ruggedness of a 
traditional SUV.” <br>
<br>
The projector beam halogen headlamps optimise illumination for safer night 
driving. The Xenon headlamps give a broader low beam and better curbside 
visibility. The beam light is directed to improve driver visibility and reduce 
the risk of glare to oncoming traffic. It is fitted with headlamp washers to 
maximise illumination from the headlamps as the high pressure cleaning system 
keeps the headlamps free of mud and road grime. Exterior mirrors can be adjusted 
with a simple multi-directional switch for optimal rear-view angles. A separate 
switch controls heating of the outside mirrors. In-glass turn-signal indicators 
alert other drivers of one’s direction intentions.<br>
<br>
As one moves into the SUV, one immediately notices that the cockpit-inspired 
interiors are quite driver-oriented. The new instrument panel’s center stack and 
controls are angled towards the driver. Like the exterior, it features many of 
Saab’s traditional, brand-distinctive design elements. <br>
<br>
On the safety front, 9-7X features a comprehensive safety package standard 
across the range, including dual-stage frontal airbags, head curtain side 
airbags like the Volvo XC90, a passenger sensing system, and a rollover sensing 
system.<br>
<br>
The Saab 9-7X comfortably seats five adults. Like most luxury SUVs, it comes 
with all the latest amenities, including heated leather seats, automatic climate 
control, a driver information computer, and wood accents. It holds up to 41.0 
cubic feet of cargo space with the rear seat back up and 80.1 cubic feet with 
the seatback folded down. <br>
<br>
Without doubt, the vehicle sets one apart from most of the others on the road. 
However, its price tag — at RO 17,000-plus — may not really appeal to many of 
the Saab lovers.</font></p>
