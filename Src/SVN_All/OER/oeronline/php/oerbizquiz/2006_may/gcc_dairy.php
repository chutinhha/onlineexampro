<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<img border="0" src="uae_flag.jpg" width="198" height="44"><br>
Growth in the manufacturing sector in the UAE hardly gets noticed as tourism and 
construction activities hog limelight. However, this sector is doing fairly 
well. According to the Ministry of Finance and Industry, investment in the UAE’s 
manufacturing sector rose to US$ 250.49 billion last year. In 2005 alone, 
investment was US$ 19.466 billion, up 8.4 per cent over the previous year, At 
19.2 per cent, manufacturing is the second largest contributor to the UAE’s GDP. 
Year 2004 saw investment in the manufacturing sector grow by over 44 per cent 
from US$ 160.1 in 2003 to US$ 231.13 billion in 2004.<br>
<br>
The largest sector in terms of investment within the manufacturing industry is 
the food and beverage sector with 299 industries accounting for the lion’s share 
of the investment with US$ 114.50 billion or 45.8 per cent of the total 
investment since the Federation was established in 1971, followed by US$ 55.05 
billion or 22 per cent in chemical products. The food and beverages sector 
recorded 200 per cent increase in its investments in 2004 over 2003. <br>
<br>
Even with the highest number of manufacturing units, metal and machinery sector 
(858 units) followed by chemical products (589) failed to attract huge 
investments. Same is the case with lumber and furniture sectors, that attracted 
the lowest investment, just US$ 734 million. But the same sector registered 
highest maximum growth in 2004 over 2003 with a 15.3 per cent growth in 
employment, followed by non-metallic mineral products with a 13.8 per cent 
growth, and food and beverages 10.9 per cent</font></p>
