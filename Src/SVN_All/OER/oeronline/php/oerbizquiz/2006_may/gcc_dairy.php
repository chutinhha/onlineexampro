<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<img border="0" src="qatar_flag.jpg" width="246" height="55"><br>
A highly insightful and slightly controversial report on Qatar’s labour market 
has been released, and the country’s Planning Council is trying to garner 
support to implement its recommendations. The World Bank authors of this recent 
report, commissioned by the Planning Council, warn that major deficiencies in 
the workforce deployment system, particularly public education, must be 
addressed.<br>
<br>
Globally, unemployment is on the rise, but in Qatar, the demand for jobs far 
outweighs the supply. It is estimated that the demand for jobs in the country 
will increase by thousands every year, and the country’s biggest challenge will 
be in meeting this need. The Council invited some of the world’s top experts to 
study the local labour market. The World Bank’s analysis was blunt:<br>
• The Qatari labour force is neither large enough nor suitably qualified to 
sustain the size and specialized nature of the economy that is generated by the 
oil and gas sector<br>
• The deployment of the Qatari labour force is further constrained by an 
eight-fold segmentation and by poor outcome of the workforce deployment system, 
especially with respect to the education of Qatari men<br>
• The ability of the labour and education authorities to coordinate within and 
between themselves, and to jointly guide the development of the workforce, is 
hampered by policy, organizational and administrative impediments and 
statistical inadequacies.<br>
<br>
The Qatari population, the report claims, is not large enough to support the 
economy shaped by the oil and energy sectors. Between 1997 and 2004, the annual 
population growth rate of Qataris has been 2.2 per cent compared to 6.5 per cent 
for non-Qataris. There is a large male deficit in education that prohibits 
Qataris from deservedly replacing non-Qataris. For example, the enrolment rates 
of Qatari men in secondary education are already lower than those of women; the 
examination pass rates and progression rates from one education level to another 
is also lower than men; there are nearly three female university students for 
each male student at Qatar University; by age 25, Qatari males have already lost 
nearly 6,000 education years compared to Qatari females; and there are (in 2004) 
nearly 1,200 fewer Qatari male workers with university education than Qatari 
women.—Vani Saraswati</font></p>

