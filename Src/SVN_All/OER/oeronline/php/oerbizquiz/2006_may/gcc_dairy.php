<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<img border="0" src="kuwait_flag.jpg" width="182" height="44"><br>
Despite strong financial position, GDP growth rate is expected to slow down in 
Kuwait unless the pace of structural reforms accelerates, says an International 
Monetary Fund (IMF) report. Although the overall fiscal position is projected to 
remain in a comfortable surplus over the medium term, the report suggests Kuwait 
to consider improvements in the structure of the budget by gradually increasing 
capital expenditure, rationalising transfers and subsidies, and achieving a 
better balance between productive expenditure and fiscal saving. IMF directors 
noted that efficient management of the rapidly growing public sector savings 
would be the key to Kuwait’s long-term fiscal viability.<br>
Overall, Kuwait registered a strong macroeconomic performance during 2004 and 
2005. Real GDP expanded at an average rate of 7.5 per cent, thanks to higher oil 
production and buoyant non-oil activity (6.4 per cent). Per capita GDP increased 
by 43 per cent to more than US$26,000 during the two-year period through 2005. 
Inflation remained low at 1.3 per cent in 2004, although it rose to almost 4 per 
cent in 2005, due to rising non-oil activity, abundant liquidity, and the 
delayed effect of the depreciation of the Dinar against the Euro and Yen.<br>
<br>
On the fiscal front too Kuwait showed improvement. The overall fiscal surplus — 
estimated at 24 per cent of GDP in 2004-05 and a saving rate of 28 per cent of 
GDP in 2004-05 — was the highest among all GCC countries. The “external current 
account” annual surplus averaged around US$ 25 billion (35 per cent of the GDP), 
says the IMF report. At an annualized rate, exports of oil and oil products 
increased by over 47 per cent, but non-oil export growth slowed down to 15 per 
cent compared with a surge of about 70 per cent in 2003, because of slower 
growth of re-exports to Iraq.<br>
<br>
However, IMF cautioned the authorities over the expenditure pressures, which are 
already evident in the area of wage and salary increases and higher subsidies 
and transfers. In the absence of a clear strategy for utilising the surpluses, 
pressures would intensify for higher salary increases, a larger size of the 
government, and larger transfers to Kuwaiti nationals, the IMF directors opine. 
While supporting the authorities’ plan to build up Reserve Fund for Future 
Generations, IMF has recommended that there be a “Value Added Tax” to increase 
non-oil revenues as some GCC countries have done.<br>
<br>
While welcoming the steps taken to further strengthen banking supervision and 
open further the sector for foreign banks, IMF recommends, an expeditious 
drafting and passing of the capital market and insurance laws; and establishing 
the capital market and insurance authorities to ensure comprehensive oversight 
of the stock exchange, investment companies, and the insurance sector. To 
further promote private sector participation, IMF has urged the authorities, to 
apply the Kuwaitization policy with flexibility, so that the competitiveness and 
profitability of the private sector are not adversely affected.
</font></p>
