<br>
</font><font face="Verdana" style="font-size: 14pt"><b>Forex reserve dilemma</b></font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="gcc_dairy1.jpg" align="right" width="200" height="229">Central 
banks of various Middle Eastern countries are seriously mulling over 
diversifying their foreign exchange reserves from US Dollar to Euro. Mixed 
opinions were expressed by the Middle Eastern central banks on the issue. While 
the UAE and Qatar, followed by the Kingdom of Saudi Arabia are seriously 
considering shifting reserves from US Dollar to Euro, Bahrain and Oman are still 
keeping faith on US Dollar, but are open to shift. However, the central banks 
will take the final decision on the issue soon.<br>
<br>
All the Gulf Cooperation Council (GCC) countries have pegged their currencies to 
the US Dollar. The Gulf central banks are increasingly toying with the idea of 
hiking the share of Euro in their reserves, thanks to the major gains made by 
the Euro in the recent months and the overall negative long-term outlook of the 
US Dollar. All the Gulf countries hold over 70 per cent of their reserves in US 
Dollars. The six Gulf central banks together hold more than US$ 250 billion in 
reserves.<br>
<br>
The United Arab Emirates has already announced its intent to augment the portion 
of its forex reserves held in Euro-denominated assets from 2 per cent to 10 per 
cent. The head of the UAE central bank, Sultan Nasser Al Suweidi, said the bank 
was mulling over converting 10 per cent of its reserves from US Dollars to 
Euros. Critics say the UAE’s decision is a reaction to the Dubai Ports fiasco. 
However, UAE officials have repeatedly clarified their stand and said if they 
move out of US Dollar, it will be because of market dynamics rather than because 
of politics. Such a move would reduce the risk that its central bank would incur 
should the US Dollar weaken sharply.<br>
<br>
In response to the market volatility, it is learnt that Saudi Arabia too is 
considering not, to depend solely on the US Dollar. At a gathering of central 
bankers held recently in the UAE, the Saudi central bank governor said stability 
was a priority in his policy on asset holdings. Other central bankers said they 
were looking at diversifying reserves estimated at around US$ 50 billion. It is 
reported that already, over the past couple of years, most members of OPEC have 
slashed the proportion of deposits held in US Dollars from 75 per cent to 61.5 
per cent.<br>
<br>
“The return on the US Dollar is higher than on other currencies. It makes sense 
to be invested in the US Dollar as long as the (currency) peg is there,” said 
Hamood Sangour Al Zadjali, Executive President of Oman’s central bank, which 
keeps 70 per cent of its reserves in US Dollars and the rest in other 
currencies.<br>
<br>
Expressing similar sentiments, Rasheed Al Maraj, Governor of the Bahrain 
Monetary Agency, said: “We are heavily invested in US Dollar-based assets, and 
that is our position. We are maintaining (that) same policy.” Qatar’s central 
bank Governor said that the bank could hold up to 40 per cent of its foreign 
exchange reserves in Euros.<br>
<br>
Commenting on the Gulf countries’ concern over switching forex reserves holding 
from US Dollar to Euro, Ashraf Laidi, Chief Currency Strategist at MG Financial 
in New York said, “the fact that these Gulf nations are major producers of the 
world’s most important commodity (oil), which happens to be priced in US Dollar, 
the mere consideration to carry more Euros should help the Euro against the US 
Dollar.”<br>
<br>
Analysts feel that although the forex reserves held by the Gulf countries are 
modest compared to other countries such as China, Singapore and others, the 
decision to shift reserves will have direct impact on the other country’s policy 
decision and increase pressure on the US Dollar. <br>
<br>
The US Dollar has already started showing depreciation against most major 
currencies, including Euro, British pound, Japanese Yen, Swiss Francs, Canadian 
Dollar etc in the first week of April. According to a Dow Jones Newswires survey 
of 20 leading foreign exchange dealers, the US Dollar was seen ending the year 
considerably lower versus the Euro and the Yen.
— Sowmya Sri Rallabhandi</font></p>
