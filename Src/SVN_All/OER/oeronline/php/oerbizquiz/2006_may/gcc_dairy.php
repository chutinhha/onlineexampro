<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
Saudi Arabian banks have shown impressive performance in 2005. Net profits of 
the banks posted a record increase of about 60 per cent, totaling US$ 100.87 
billion in 2005. At least nine banks profits exceeded US$ 3.75 billion. Overall 
the banks registered an annual growth ranging between 36.5 and 40 per cent.<br>
<br>
<img border="1" src="profit_chart.jpg" align="right" width="200" height="208">The 
banks that showed considerable growth profits are Al Rajhi Bank, which topped 
with US$ 21 billion, an increase of 92 per cent over the preceding year. This 
represents 21 per cent of the total net profit of the banking sector, followed 
by National Commercial Bank (NCB) with 18 per cent profit, and Samba with 15 per 
cent of the total profit. The combined net profit of Al Rajhi, NCB and Samba 
makes up 54 per cent of the total.<br>
<br>
Individual performance of the banks are as follows: NCB profits rose by 41.9 per 
cent reaching US$ 18.75 billion; Samba’s net profit rose 60 per cent to over US$ 
15 billion; Riyad Bank, which comes fourth with 13 per cent of the total net 
profit registered 41.5 per cent increase with US$ 10.5 billion; and SABB (Saudi 
British Bank) and Saudi Fransy Bank hold fifth and sixth positions with US$ 
9.375 billion and US$ 8.25 billion (52 and 44 per cent) respectively. <br>
<br>
Arab National Bank holds seventh position with US$ 6.825 billion, an increase of 
56.6 per cent, followed by Saudi Investment Bank and Saudi Hollandi Bank with 
US$ 3.975 billion and US$ 3.93 billion (3.95 and 3.9 per cent of the total 
banking net profit) respectively.
</font></p>
