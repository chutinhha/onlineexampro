<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<img border="0" src="bahrain_flag.jpg" width="244" height="44"><br>
The financial services industry is creating more employment opportunities for 
the Bahrainis. The industry is dominated by the nationals who hold about 70 per 
cent of the jobs in the financial institutions in the Kingdom. The total number 
of nationals employed is up from 5,581 to 5,951, a 6.6 per cent rise, according 
to a survey carried out by the Bahrain Monetary Agency’s (BMA) Financial 
Stability Directorate.<br>
<br>
Significantly, employment in Bahrain’s financial services industry grew by 9 per 
cent last year to a total workforce of 8,219 people, up from 7,542 a year 
before. Also, the number of Bahraini women working in the commercial banks 
increased by 19.7 per cent. According to the BMA survey, employment in Bahrain’s 
banking sector grew by 10.6 per cent and the total Bahraini workforce grew by 
7.6 per cent, which represented 75.5 per cent of the total workforce in this 
sector. Of the Bahraini personnel, nearly one-third are women while the 
remaining are men. Interestingly, the Bahrainis form 87.1 per cent of the total 
commercial banks workforce, 61.9 per cent of offshore banking units employers, 
while 69.4 per cent of the investment banks employers are Bahrainis.— Sowmya Sri Rallabhandi</font></p>
