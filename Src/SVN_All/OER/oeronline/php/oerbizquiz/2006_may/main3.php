<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" style="font-size: 14pt">
<br>
</font></b><font face="Verdana" style="font-size: 14pt; font-weight: 700">
Megabucks await Omani sportspersons</font><font face="Verdana" size="1"><b><br>
The emergence of local sporting talent in Oman provides an opportunity to the 
corporate sector to use their popularity to pep up brand sales </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt; font-weight: 700">G</font><font face="Verdana" size="1">lobally, 
sports celebrity endorsement is a big business. However, the trend is yet to 
catch up in Oman. Factors like dearth of Omani sports celebrities, small 
consumer market size and absence of professional sports management agencies may 
have been the stumbling blocks. But things are changing fast. As Oman gets more 
integrated into the global economy, the market for local sports celebrities is 
beginning to warm up. <br>
<br>
<img border="1" src="inthenews1.jpg" align="right">Momentum PR, a Muscat-based company, is trying to fill in the hiatus between the 
rising Omani sports personalities and brand marketers. “Our agency assists the 
Sultanate’s very own internationally acclaimed footballer Ali Al Habsi, Oman’s 
No. 1 tennis player Fatma Al Nabhani, and the accomplished rally driver Nizar Al 
Shanfari,” says Sarah Livingston, Managing Director of Momentum PR, while 
talking about the sports celebrities who have signed up with their agency.<br>
<br>
It is an accepted fact that local sports 
stars have far greater impact on the local audience compared to those borrowed 
from the overseas market. In recent years, several Omanis, including Ali, Fatma 
and Nizar, have fast emerged as local sports celebrities and potential brand 
ambassadors. This, therefore, increases the opportunity for local organisations 
to appoint Omani sporting role models to represent their products and services.<br>
<br>
However, it is a gradual and difficult process. “As of now, there are only a few 
high-profile Omani sporting stars. So the appointment of brand ambassadors has 
never seemed a viable communication tactic,” says Livingston. “However, times 
are changing. The sporting talents of the Sultanate need support. There is great 
scope for mutually beneficial relationships between the local talents and the 
big brands,” adds Livingston.<br>
<br>
“Our first such project was to manage the high profile sponsorship deal between 
OOMCO and Ali Al Habsi,” Livingston informs. “For OOMCO (otherwise known as Oman 
Oil), this project was a huge success. The locals started identifying Oman Oil 
with Ali in the spirit of public goodwill and charitable alliances,” Livingston 
asserts.<br>
<br>
<img border="1" src="inthenews2.jpg" width="150" height="200" align="left" hspace="5" vspace="2">The recent emergence of Fatma, a young and talented semi-professional tennis 
player, showcases the valued contribution that woman sportspersons can make in 
Oman. If she continues to perform well, she has the potential of becoming an 
icon to all women in Oman, as well as the entire region. 
<br>
<br>
Likewise, Nizar has been a consistent performer on the regional rally circuit. 
With his recent number one position and ‘Champion of the Oman Rally’ title, he 
now enjoys a huge fan following. <br>
<br>
In most cases, the association of sports celebrities with a product works only 
if there is perfect brand fit between them. “With Ali Al Habsi and OOMCO, the 
brand fit was exceptional. Like Ali, OOMCO is still a relatively young brand. It 
looks to be perceived as a youthful, Omani, energetic, and dynamic oil marketing 
company. OOMCO also identified football as its focus for sponsorship investments 
as the game appeals to a large section of their target audience,” emphasizes 
Livingston.<br>
<br>
As seen in the other markets, the local companies in Oman are expected to 
increasingly engage the homegrown sports talents to enlarge their market share. 
As per the information available with OER, a leading international brand from 
the GCC is expected to sign up as a sponsor of the national football team of 
Oman soon. <br>
<br>
The way the trend is changing, it will be no surprise if an Omani sportsperson 
lands up with a highly lucrative, multi-million Rial endorsement deal. </font></p>

