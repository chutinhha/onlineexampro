    <p align="center" style="margin: 10" dir="ltr">
    <font face="Verdana" style="font-size: 14pt" color="#CC3333"><b><br>
    Empowerment, 
    the e-governance way <br>
&nbsp;</b></font><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" style="font-size: 14pt"><b>
    <img border="0" src="egov.jpg" align="left" hspace="5" vspace="2"></b></font><font face="Verdana" size="1">In April this year, Oman took another major 
    step towards puting the e-governance system in place with the launch of the 
    eOman campaign. Though Oman has many more milestones to cross before 
    becoming an e-governance society in the true sense, such developments build 
    the confidence that the Sultanate is moving in the right direction. eOman’s 
    vision is to “create an effective government-community-citizen 
    infrastructure that provides better public services to the people, resulting 
    in a meaningful information flow between the government and citizens.”<br>
    <br>
    As we move along the e-governance process, we must ensure one thing: people 
    must understand that e-government is not a substitution for good governance. 
    As a matter of fact, e-governance basically facilitates governance. It does 
    not automatically mean that if you have e-services, you will have good 
    governance. <br>
    <br>
    The success of e-governance depends immensely on how serious the 
    decision-makers are towards it. It hinges on the creation of channels for 
    the citizens to give their feedback on the government’s performance and 
    participation in the public policy. Satisfaction of citizens should weigh 
    heavy in the same way as companies care for the satisfaction of their 
    customers. Citizens should make best use of the available means to send 
    across the message. In the absence of this, e-governance can turn out to be 
    only a cosmetic exercise, as seen in many of the so-called e-governed 
    societies.<br>
    <br>
    Take the case of the US, which is widely recognised as the top e-governance 
    country in the world. Thanks to the latest technology, Hurricane Katrina was 
    accurately predicted before it happened. Yet, the government machinery 
    failed to react and respond to the disaster appropriately. They could have 
    circulated the information online. This could have saved thousands of lives 
    and billions of dollars. But it did not happen.<br>
    <br>
    In our neighbourhood, Dubai has done a commendable job in implementing 
    e-governance. Its e-governance portal - www.dubai.ae - has introduced a 
    range of services hitherto done conventionally. The website attracted a 167 
    per cent growth in traffic in 2005. The Dubai Municipality has achieved more 
    than 1.6 million online transactions and collected over 14 million Dirhams 
    through e-payments.<br>
    Like Oman and Dubai, many Arab states are recognising the importance of 
    e-governance. More importantly, they have come together to co-operate on 
    this. Digital Arab Solidarity (DAS), comprising of Arab nations, was formed 
    recently. Its members have met twice,. In the last meeting, DAS recommended 
    formation of an Arab Fund for development of digital governance, organising 
    of a regular regional Arab Forum on e-governance observation and the 
    creation of an e-governance Arab experts’ network. The proposal is expected 
    to be forwarded to the ministerial meeting scheduled to be held in Sharm 
    el-Shaikh shortly. The participating countries have agreed on key common 
    strategies for developing an e-government action plan. Each country will 
    customize it as per their needs. On the anvil is a portal for Arab 
    e-governments under the supervision of Dubai School of Government. It will 
    have features like links to Arab e-governments. <br>
    <br>
    DAS is going to face a number of stumbling blocks. Perhaps, the biggest 
    challenge will be the existing legal framework in the Arab world. The 
    Western countries, which are at the forefront of e-governance, had faced the 
    same dilemma. They took a long time to untangle (it’s not over yet!) the 
    legal quagmire such as the credibility of electronic transactions. <br>
    <br>
    The Arab world needs to sort out some issues, like whether email could be a 
    signed and tangible letter, or the legal bearing of e-transactions or 
    redressal of online grievances. New technologies such as fingerprint 
    scanning technology may also ease the way. For electronic migration, the 
    legal loopholes have to be sorted out. This will instill confidence among 
    the citizens.</font></td>
