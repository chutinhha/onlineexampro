</font><font face="Verdana" style="font-size: 14pt">
<b>Jog for focus</b></font><font face="Verdana" size="1"><b><br>
Karnail Singh Cheema, Senior Vice-President (Legal and Projects) of the Omar 
Zawawi Establishment is a busy executive. Time, for him, “is a very scarce 
commodity”</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b><img border="0" src="pasttime.jpg" align="left" hspace="5" vspace="2">P</b></font><font face="Verdana" size="1">rior 
to his current assignment, Cheema worked in India as Chief Legal Officer for 
Ispat Industries. He joined the OMZEST group in the year 2000 and is on the 
Board of several group companies. His expertise in handling all aspects of 
project development — right from conceptualizing, designing, coordinating to 
finalizing the deals — leaves him very little time for leisure. The hectic pace 
at work also creates unwelcome stress. <br>
<br>
Cheema travels frequently. So weekends are religiously earmarked for quality 
time with his wife and son. What, then, is the motivation for a leisure time 
activity? “I firmly believe that a healthy mind stays in a healthy body. So my 
first priority after my official assignments are over is my fitness regime. I 
manage time to do that. It helps me to completely de-stress.” <br>
<br>
An ardent jogger, Cheema jogs about 10 km at least four times a week. “I am so 
happy and involved with my jogging routine, taking in the fresh air and 
relishing the natural beauty of the sea and mountains here in Muscat that many a 
time I fail to notice people. And they often complain for that! But I go back 
home totally refreshed. Very often, I find solutions to my work problems while 
jogging as I have 100 per cent concentration then!”<br>
<br>
Cheema has been jogging for the last 20 years now. “Earlier, I used to jog for 
only 4-5 km but my stamina has improved over the years. Now I can do a 10-km jog 
in 55 minutes straight, which I think is good for my age,” he declares proudly.
<br>
<br>
So what actually pulled him towards jogging? “I was a tennis player. I have 
represented my College in 1984 at the state level in India. I have also played 
in the Maharashtra Amateur Tennis Tournament in 1988-89. However, I developed a 
knee problem and started going to the gym regularly thereafter. Since I am in an 
office environment all day, I like to get a bit of fresh air, which jogging 
provides. I also travel a lot. All I need is my shoes and shorts and I can 
unwind in whichever city I am!” he concludes. <br>
<br>
Besides this strenuous pastime, which actually leaves him refreshed, Cheema 
enjoys reading to keep himself abreast with the latest in business and politics. 
He also loves meeting people and is currently writing a book on the various 
facets of human personality gathered from his vast experience of dealing with 
various people and lessons learnt thereof. Good luck for that! — Uma Devi Jadhav</font></p>
