                  <td width="100%"><img border="0" src="../../images/2002_june/pg24.jpg" align="left" hspace="5" vspace="5" width="225" height="139"><font face="Verdana" size="2" color="#C0C0C0">::

                    Cover Story<br>

                    </font><font face="Verdana" size="4" color="#FF0000">The Precious find</font>

                    <p><font face="Verdana" size="1"><b>Formula One motor racing is watched by more people each year than any other sport. No wonder Bahrain is so keen to hold the first Grand Prix in the Gulf. OER's Bahrain correspondent Nabs Al Busaidi reports</b></font></p>

                    <p><font face="Verdana" size="2">At a time when the need for economic diversification among all the Gulf nations is becoming increasingly critical, Oman, after encouraging the development of its manufacturing and tourism sectors in the past few years, is all set to open up its mining industry to foreign participation. For the purpose, the government will soon announce the amendment of its existing mining law. The Sultanate's strategy, which is essentially devised to align itself with the mounting demands of World Trade Organisation (WTO), also aims to give the foreign investors the same benefits enjoyed by the local companies.<br>
                    Although declining to comment on the precise contents of the law, Hilal Al Azri, Director General Minerals, Ministry of Commerce and Industry (MOCI), informs that the government will allow full foreign ownership in the mining projects, and a considerable reduction on taxes and royalties in order to make the incentive structure attractive. "The old law has now been reviewed and updated, and is awaiting the government's approval," he said.<br>
                    Presently, investors are liable to pay an application fee of RO25,000 and a prospecting permit fee of RO50,000. In addition to this, the companies can also obtain a quarry permit and mining lease, depending on the nature of the mining activities. As far as royalties are concerned, the existing structure calls for the investors to pay two per cent on the net value of industrial minerals, and five per cent on metallic minerals, and 15 per cent on diamond, ruby and other precious stones.<br>
                    The changes in the new law may include a proposed reduction of royalties to two per cent of the net value on all minerals, and five per cent on diamonds and precious stones. Official sources indicate that the government is also proposing to put additional resource charges, which are taxable on cash flow that remains after the deduction of royalty and income tax, provided the internal rate of return (IRR) is more than 20 per cent of the initial funds laid out for investment and development.<br>
                    <br>
                    <b>Potential galore</b><br>
                    Lack of appropriate surveys has been one of the major stumbling blocks to the progress of the mineral industry in Oman. Experts argue that the country needs to extensively survey and quantify all the resources. According to Dr Khalid bin Hilal Al Rawahi, minerals lecturer and researcher at the Sultan Qaboos University (SQU), there are many independent surveys done by different agencies but we still do not know the exact quantity of the mineral reserves. "What we have now are only rudimental statistics which won't help us in our quest to seriously attract foreign investors," he says.&nbsp;<br>
                    A 1997 government sponsored survey showed mineral deposits covering an area of up to 4,000 sq km (1,540 sq miles) in total, but didn't quantify the total reserves. The state-run Oman Mining Company (OMCO) has been mining since 1983, but with modest success. OMCO exported 20 million tonne of minerals including copper and silver between 1983 and 1994, before sites in Sohar were depleted, whereas Oman Chromite Company (OCC) was mining chromite that was entirely destined for export markets. OCC exploited the Ghashabi-2 deposit, producing 18,000 mt of chromite in 1997. At least 4,000 tonne were exported to Japan, and unknown quantities were exported to the Refractories Corporation of the Philippines. Copper cathodes, processed from imported concentrates were exported from the port of Majis.<br>
                    However, extensive explorations in the 1990s conducted by France's Bureau de Research et Minieres (BRGM) and Japan International Cooperation Agency (JICA) in search for the illusive gold paid off. The efforts led to an exciting discovery of 12 million tonne of gold and copper deposits at Rakah and Hayl Al Safil in the northeast region. JICA, which also advises the government on mineral exploration, has recently identified various areas that hold an estimated 15 million tonne of copper. Potential for diamond has also been discovered recently at Haushi-Huqf in the east of Oman by an airborne magnetic survey, but the government was unable to quantify the find.<br>
                    OMCO is currently mining a site in Rakah producing 800,000 kg gold per year, establishing itself as the only company to export gold in the country. The company also has a smelter facility at Lasail near Sohar, and is currently refining imported copper from South American countries, Africa and Iran at a rate of 20,000 tonne per year. Earlier, the aforesaid copper smelter and refinery imported copper concentrates for toll and custom smelting. "We see it as another attraction for investors who won't have to send their raw materials abroad for refining," says Azri. Also, since most of the mines are located in and around Sohar, investors would have easy access to the upcoming Sohar port, which is expected to be open for business from April 2003.&nbsp;</font></p>
                    <p align="center"><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg27a.jpg" width="350" height="254"></font></p>
                    <p><font face="Verdana" size="2">Just a few years back, the Directorate General of Minerals had started to prepare six concession blocks in the north of Oman for bidding by private companies for copper and gold exploration. So far, the only concession granted for exploring metallic minerals has been awarded to a private Omani firm called National Mining Company (NMC), a subsidiary of the local business group MB Petroleum. NMC has signed an exploration agreement with the government in October 2000 to explore minerals at Block-1 in Shinas in the north, covering an area of 364 sq km. The concession is 100 per cent owned by the company, and as per the agreed terms, it would spend RO180,000 on exploration.<br>
                    "We have spent around RO800,000 on exploration so far, and have discovered 2.5 million tonne of gold and copper deposits with a current market value of US$100 million (RO40 million)," says Bob Close, General Manager of NMC. "By exploring only five per cent of our concession area, we have only scratched the surface and there's a lot yet to be found. There could be as much as 30 million tonne of gold and copper deposits" he added.<br>
                    NMC hopes to follow OMCO's suit but needs to complete a feasibility study for its Block-1 discovery before it could apply for a mining licence. The company plans to handover the study to the government in September and expects permission to be granted to start mining next year.<br>
                    Also, Oman's natural wealth is not confined to the glittering metal. The discovery made during 1980s showed that coal deposits near Sur in the Wadi Fisaw and Muswa, and in the eastern region, have reserves of over 122 million tonne, which could provide about 300 megawatts of power over a period of forty years. But plans for coal mining have been shelved mainly because electricity in the Sultanate is generated from gas, diesel and desalination plants.<br>
                    Academic researchers opine that the government must also consider the environmental aspects in the new mining act. "The impact of mining on environment is a serious one. In the past, the mining areas in Sohar suffered considerable pollution exposure from mineral projects. The government must include a clause for a private company to submit a report on environment before mining starts," observes Rawahi.&nbsp;<br>
                    <br>
                    <b>A source for diversification</b><br>
                    There are two types of minerals found in the Sultanate - industrial and metallic.
                    <img border="0" src="../../images/2002_june/pg27.jpg" align="left" hspace="5" vspace="5" width="225" height="200"> Industrial minerals like marble, silica, gypsum and limestone are located almost all over the country, with Samail in the Dakhliya region holding greater potential. The Omani mountains, according to a 1994 geophysical survey, have a higher degree of industrial minerals in the mountains than in the flat areas. The metallic minerals include gold, silver and platinum, and are currently being mined only in few areas. According to a 2001 report released by the Directorate of Mineral Exploration, among the metallic minerals, only copper, chromite and gold have so far been mined. Other commercially viable mineral ventures include the smelting of copper, gold and silver as by-products. Geologists believe that Oman is still largely untapped as far as its mineral wealth is concerned, and that it needs to vigorously step up its efforts to explore deeper. "More efforts are needed to cover the whole country. The potential is large enough to support the Sultanate's economic diversification plans," says Yashiaka Shibata of Japan's Metal Mining Company (JMMC).&nbsp;</font></p>
                    <p align="center"><font face="Verdana" size="2"><img border="0" src="../../images/2002_june/pg28.jpg" width="200" height="264"></font></p>
                    <p><font face="Verdana" size="2">Sharing a similar view HE Ahmed bin Abdulnabi Macki, Minister for Economy, believes that mineral wealth will significantly contribute to the country's plans of reducing dependence on oil revenues. ÒWe welcome the efforts in the mineral sector. In short span of time, we are going to see a significant shift in the income balance between oil revenues and the earnings from non-oil sectors such as mining," he observes.<br>
                    Official statistics suggest that the country earned RO30 million in 2000 from minerals, although the earnings for 2001 have not yet been published, it is estimated that the revenues stood at RO27 million as of October last year. Oman, like its Gulf Cooperation Council (GCC) partners, heavily depends on crude oil exports, which make up around 70 per cent of national income.<br>
                    "The biggest opportunity for Oman to increase revenues should come from industrial minerals, as we have a huge reserve of such minerals that have not yet been fully explored. The metallic mineral reserves are quite low and their commercial prospects are good as well," explains Rawahi.<br>
                    On the other hand, bankers suggest the new mining act needs to be aligned with the government's privatisation plans. "There have been rave reviews on how Oman managed to attract foreign companies to invest in power," says a senior banker at BankMuscat. "It will all depend on the mining law and how attractive it is to investors to match that success. The government will also need to review its decision to privatise OMCO to improve its efficiency. Besides, large foreign companies have the experience, technology, and the funds to explore, besides mining competently," he adds.<br>
                    The projects would need funds and local financial institutions will certainly benefit. Overall, the private sector will receive a shot in the arm to lift it from the current slump, the banker said.<br>
                    Oman has already reserved 20 blocks to the private sector but Close hopes the new mining law will be more investor-friendly, adding that officials must give more freedom to private investors. "Licensing should be cheaper. Investors are now being asked to pay about ten times the rate they would normally pay elsewhere in the world. At the moment, the government allocates certain areas for exploration instead of giving investors the freedom to choose what they think is feasible. Another thing they need to do is to reduce bureaucracy. Investors would rather focus on exploration rather getting tangled in red tape," he laments.<br>
                    Manpower experts say that mineral projects have the potential to create jobs and boost economic activities in small towns and rural areas. "Mineral projects will also create job opportunities to for the people living near the mining areas," says Abdullah Al Harthy, manager of Muscat Manpower Consultancy said. At present, Omanisation levels in the mining industry are subject to only two companies that are actively involved in the sector. OMCO employs nearly 90 per cent Omanis while NMC has one Omani geologist out of five employees. "These types of projects are important to community development. Look what Oman LNG has done to Sur. Local businesses in Sur have received a tremendous boost ever since the project started," Harthy added.<br>
                    All in all, the mineral industry is certainly all set for the big change once its shackles are broken and foreign companies are allowed to participate freely. But it remains to be seen how attractive the incentive would be and whether foreign companies would come forward and put their funds in the mineral sector. Meanwhile, investors are hopeful that the new mining act will lift the current barriers.&nbsp;</font></p>

