                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Betting on e-banking</font><p><font face="Verdana" size="1"><b>BankMuscat gives e-commerce a new dimension.
                    OER reports</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    <img border="0" src="../../images/2002_june/pg32.jpg" align="left" hspace="5" vspace="5"></font><font face="Verdana" size="2">BankMuscat is all set to exploit growth possibilities in e-commerce. The bank has introduced a new product called Safecard, which helps Internet users to shop without giving out the details of their credit card. The card does not have a physical identity, as it is created by virtually using advanced software. Moreover, the actual card number never travels over the Internet, so it cannot be hacked and it is valid for a single transaction only. The system generates a new card each time one enters into a new transaction.<br>
                    "A physical credit card can be stolen or its number hacked by a fraudster, but a Safecard has no physical existence and the merchant never sees its number," said Girish Salian, Head of Card Services, BankMuscat. The Safecard at the moment, is exclusively issued by BankMuscat, and is branded with Visa International, the world's leading credit card establishment.<br>
                    Another first from the bank is the introduction of advanced technology kiosk banking, which offers customers banking facilities at a location outside the bank's branches. Except for cash withdrawal, a customer can perform most other functions at convenient locations and at his own leisure.<br>
                    As Kiosks have complete PC-styled keyboards, the customer can perform functions, which normally cannot be performed by ATMs due to their limited function of their keyboards. Moreover, non-customers can have access if they wish to find out about the various products offered by the bank.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_june/pg32a.jpg" align="left" hspace="5" vspace="5">At present, there's only one such facility in the country operated by BankMuscat. The bank will shortly install kiosk facilities in prime locations such as shopping malls.<br>
                    <br>
                    <b>Tie-up with Omantel</b><br>
                    In a bid to secure Internet financial transactions, the bank would be joining hands with Omantel to introduce an e-payment scheme for the first time in the country. "The e-payment gateway will utilise a highly secure PKI and digital certificate platforms. Transactions of any Internet shoppers will be routed from a trader's business website to the payment infrastructure at our acquiring system," says Ahmed Al Ojaily, Head of IT Research and Development.</font></p>

                    <p><font face="Verdana" size="2">The e-payment would cover transactions ranging from Internet shopping to bill payment in a very secure environment. Combined with Safecard, the e-payment would ease the pressure of both business establishments and customers by speeding up the transaction process since only one gateway would be used.<br>
                    The Bank's partnership with the telecommunications provider stretches further to
                    <img border="0" src="../../images/2002_june/pg33.jpg" align="right"> the utilisation of the short messaging service (SMS) system. SMS banking would offer usual banking services, for instance, account balance transmitted straight to your mobile telephone. "SMS banking will appeal to the customers who have no time to come to our branches. In our development of this facility, we will also send alert messages to our customers informing them about any irregularities in their transactions in the quickest possible manner," Ojaily says.<br>
                    "Our customers wherever they are, can now perform various financial transactions such as funds transfer, utility bills and credit card payment from the comfort of their homes, Moslem Al Jahdhamy, e-Banking Head said.&nbsp;<br>
                    "They can also access account statements, details, exchange rates and report ATM card loss. All the transactions are secured by the use of dual password protection."<br>
                    <br>
                    <b>Eyeing Overseas Markets</b><br>
                    The bank is eyeing the Indian Ocean countries for expansion including countries in the Gulf region and South Asia. At the moment, the bank is exploring opportunities to expand in the UAE and increase business presence in India through the establishment of a subsidiary. The bank also sees the future growth focused on allied financial services in Oman like stock brokerage, insurance and leasing. "We are financing the Omani India Fertiliser Project, Oman Air's aircraft financing and new Oman Airport Services project. We are also looking at opportunities to finance planned ventures in the fertiliser, methanol, refinery and polypropylene sectors," said Ahmed Al Abri, General Manager.<br>
                    <br>
                    <b>Financial's prospects</b><br>
                    "BankMuscat expects to do well in 2002," says Al Abri adding that, "our performance to date is within our expectations". Earlier, the bank reported a net profit of RO 7.75 million in 2001 compared to RO 16.3 million in 2000.&nbsp;<br>
                    Al Abri attributes the Bank's low profit last year to a global Economic slowdown. Al Abri expects the world economy to recover sometime this year to lift the profitability of the financial institutions in the country and reduce bad loan provisions, which hampered the banks' performances in 2001.<br>
                    "Borrowers with a strained repayment capacity can improve their financial position with a turnaround in the economy. So the loan loss provisions made by banks yesterday, can be today or tomorrow's profits," he said.</font></p>

