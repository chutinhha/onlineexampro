				<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				</font><font size="2" face="Verdana"><b>Taking Charge<br>
				</b></font><font face="Verdana" size="1"><br>
				<b>Fatma Al Araimi talks to John Blascos, the new General 
				Manager and Shell Country Chairman, Oman, in an exclusive 
				interview<br>
				</b><br>
				<br>
				<b>
                <img border="0" src="images/interview.gif" style="border: 1px solid #EFEFE9" align="right" hspace="8" vspace="4" width="200" height="305">Can you tell something about your educational and 
				professional background.<br>
				</b>I’m from the United Kingdom, though my name is Spanish, 
				which is misleading at times. I am a qualified engineer by 
				profession, having graduated from Cambridge. I later went on to 
				complete my Masters in Business Administration. After completing 
				my education, I joined Shell as a training engineer in drilling 
				/ production technologies in the early 80s, and have been 
				working for Shell for almost 28 years. <br>
				<br>
				<b>I am sure that 28 years with Shell has given you the 
				opportunity to work in different parts of the world. Can you 
				please share your experiences in various countries? <br>
				</b>After working in the UK, I worked as a drilling engineer in 
				Algeria. In 1985 I married and moved to work in Petroleum 
				Development Oman (PDO), continuing in the same role and later on 
				in production technology. Four years later, I moved to London 
				and took up a gas commercial role. I have worked in Nigeria as 
				the head of production technology and later moved to Malaysia as 
				a development manager at one of our units there. A period in 
				Shell’s upstream head office followed in the Hague. And now, I 
				am right here in Oman.<br>
				<br>
				<b>You have been posted twice now in Oman, in 1985 and 2003 and 
				once again this year. During this intermittent period, have you 
				been a witness to any dramatic changes in the country?</b><br>
				This is a very good question. I was first here in 1985, and 
				worked for four years, before coming back in 2003, for a 
				two-year stint. And, here I am once again. One thing hasn’t 
				changed much in Oman is the Omani warmth. The people are 
				extremely kind here. Though I must admit that Oman has 
				developed, and become quite advanced on all fronts. Personally, 
				I hope Oman will continue to strike a balance between 
				development and preserving its nature and beauty. Of course, 
				it’s going to be a difficult challenge considering the frantic 
				pace of growth being witnessed. <br>
				<br>
				<b>What’s your take on the changing oil and gas scenario here?<br>
				</b>I have been witness to the less technically challenging 
				times, when I was here in the early 80s. Now it is more 
				challenging, as we gradually move away from the so-called “easy 
				oil”, which is not unique to Oman but a common feature across 
				the world. However, advanced new technologies should help 
				meeting these challenges. As I was leaving Oman for the first 
				time, gas discovery was in a very nascent stage. We now have 
				state of the art gas facilities like Oman LNG, Qalhat LNG, etc.<br>
				<br>
				<b>Shell Development Oman has moved to a new office in Oman. How 
				is it going to impact your operations?<br>
				</b>We began here modestly by representing our parent company. 
				However, we have evolved and our activities have increased 
				beyond the limit of a representative office. We have established 
				Shell Technology Oman Center and a regional Learning Hub. Also 
				some of the business activities that used to be handled by the 
				regional office in Dubai will be done from here. Obviously as 
				you grow and add business activities and people, you need that 
				extra space but also this is a sign of our long-term commitment 
				to Oman.<br>
				<br>
				<b>What value in your opinion would be a fair price for crude 
				oil? <br>
				</b>This is a difficult question; no one can speculate what the 
				oil prices should be. Last year we have seen the price of oil 
				reaching $147 and later this year it dropped below $50. There 
				are many factors, which determine the price of oil, a fair price 
				is the price that is good for both producers and consumers but 
				also should be high enough to allow oil companies to invest in 
				major projects, which should secure the energy need for future 
				generations. We at Shell believe that the need for energy across 
				the world would double by the year 2050.<br>
				<br>
				We also believe that to meet most of this need, most would come 
				from fossil fuel. We must continue to invest in oil and gas 
				projects in order to meet the energy challenge.<br>
&nbsp;<br>
				<b>What role are you expected to play in your new position?<br>
				</b>As the GM and Shell country chairman, I will be looking 
				after our interests in Oman, while simultaneously nurturing the 
				relation between us and our stakeholders in the Sultanate. I am 
				a member of the board of directors of Petroleum Development Oman 
				(PDO), Oman LNG and Qalhat LNG (QLNG). In addition I am also the 
				chairman of Shell Oman Marketing Company.<br>
				<br>
				<b>You obviously have a strenuous job. How do you normally 
				relax?<br>
				</b>I am a sports enthusiast and I like golf and sailing. I love 
				photography and it’s the best way to capture nature and relive 
				memories somewhere down the line. I look forward to discovering 
				more of Oman during my leisure time taking in the natural beauty 
				and enjoying the warmth of the people. <br>
