                    </font><font size="2" face="Verdana"><b>What next for 
                Emerging Markets?<br>
                </b></font><font face="Verdana" size="1">
				<br>
                <b>Macro-economic developments like fiscal stimulus across 
                financial capitals of the world have led to valuation spikes 
                across developing markets and a growth in GDP<br>
                <br>
                </b>Emerging markets were the winner asset class after Wall 
                Street’s epic sentiment U-turn last March, when it became 
                evident that the Federal Reserve and the US Treasury were 
                determined to bail out the American banks as the lender, 
                guarantor and insurer of last resort.<br>
                <br>
                It is no coincidence that the Fed’s determination to act as the 
                lender of the last resort meant a dramatic plunge in systemic 
                risk metrics and a fall in the US dollar, the safe haven in 
                world finance after the meltdown of Lehman Brothers in September 
                2008. Zero interest rates, trillion dollar fiscal stimulus, 
                credit support packages and lower dollar meant the best of all 
                possible worlds for commodities markets.<br>
                <br>
                After all, even though the World Bank estimated that the world 
                economy contracted by three per cent in 2009, copper prices more 
                than doubled as industrial metal prices were goosed by Chinese 
                state hoarders and the money-printing spree of the central 
                banks. Crude oil prices rose from $35 to almost $80 even though 
                more than 160 million barrels in excess black gold and 
                distillates are still stored offshore in supertankers off the 
                coast everywhere from Dorset and Scotland to Singapore and 
                Greece.<br>
                <br>
                This macro milieu has led to dramatic valuation spikes in 
                emerging markets. I still believe it is premature to take 
                profits in most emerging markets. The Federal Reserve cannot and 
                will not raise interest rates as long as the US economy’s return 
                to growth is so fragile. Nor will the Chinese Politburo allow 
                the People’s Bank of China to tighten monetary policy as long as 
                exports have not revived and inflation is not an imminent 
                threat. Meanwhile, GDP growth in the emerging markets will 
                accelerate as global consumer demand and industrial production 
                revives.<br>
                <br>
                <b>The Russian ‘roulette’<br>
                </b>A weak dollar, strong commodities, eight per cent Chinese 
                GDP growth, no Fed rate hikes and rising risk appetites are all 
                bullish for emerging markets as an asset class. Russian equities 
                are still attractive even though the Moscow MICEX index has 
                doubled in 2009. Russia witnessed the worst economic squeeze 
                since Putin was anointed/elected President in 2000 with a 
                draconian 10 per cent contraction in 2009. However, its GDP 
                growth is expected to rise at least five per cent soon.<br>
                <br>
                The central bank spent $300bn in reserves to successfully 
                stabilise the rouble and can now safely cut the refinancing rate 
                from its current nine per cent to as low as seven per cent by 
                next summer. The Kremlin has now agreed to cut taxes on East 
                Siberian crude to encourage capex, a positive development for 
                shareholders at Rosneft and Lukoil. Above all, Russia still 
                trades at a 30 per cent discount to the Morgan Stanley emerging 
                markets index. The Templeton Russia fund (TRF) is listed on the 
                NYSE and enables investors to gain exposure to a broad spectrum 
                of Russian blue chips.<br>
                <br>
                <b>India, Turkey costly<br>
                </b>I am less sanguine on India and Turkey, two of 2009’s 
                fabulously profitable emerging markets. India is expensive at 
                Sensex 17000 or 17 times forward earnings at a time when rising 
                inflation will inevitably force the Reserve Bank of India (RBI) 
                to hike interest rates by next spring. Moreover, Indian IT 
                service colossi will be clearly hit by tighter margins, 
                decelerating earnings growth and a stronger rupee. I can easily 
                envisage a fall in the Infosys ADR listed in New York from 50 
                now to 38-42 in the next 12 months.<br>
                <br>
                Turkey’s Istanbul ISE index more than doubled in 2009 as the 
                central bank slashed interest rates by more than 1000 basis 
                points. Yet Turkey’s epic monetary ease is over and interest 
                rates could well rise by 200-300 basis points as the Ankara 
                central bank responds to inflation. This will lead to a stock 
                market correction as the ISE index has a 50 per cent weightage 
                in banks, similar to the GCC indices.<br>
                <br>
                Moreover, Turkish banks’ balance sheets are highly correlated to 
                interest rates, with almost one third in assets invested in 
                government debt securities. Since Prime Minister Erdogan has not 
                concluded a credible IMF deal, it is entirely possible that 
                global investors will take profits in Turkish equities as local 
                lira rates rise.<br>
                <br>
                <b>Clear beneficiary<br>
                </b>Hong Kong is the clear beneficiary of its peg to the US 
                dollar, acceleration in Chinese GDP and corporate earnings, the 
                lowest interest rates in the Pacific Basin and a surge in Asian 
                trade, manufacturing and a robust property market. While HSBC 
                and Standard Chartered bank’s exposure to Dubai have hit their 
                shares and therefore the Hang Seng index, I would buy the Hong 
                Kong index at 18,000 for a 25,000 target in 2010. Singapore’s 
                index EWS is also a potential Asian value play as I am convinced 
                that every correction will trigger buy on dips psychology. The 
                bull run in the asset class will continue even if it will not be 
                a bull rampage. <br>
