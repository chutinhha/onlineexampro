				<b><br>
				<img border="0" src="images/Envir-Omanoil.gif" align="right" hspace="9" vspace="5" width="120" height="181">Environmental issues are occupying the same position as routine 
				work for most of corporate Oman. Starting this month, as part of 
				Oman Economic Review’s green campaign, every month we will be 
				featuring the ‘green’ views of corporate captains. Omar Ahmed 
				Qatan, CEO of Oman Oil Marketing Company, shares his views on 
				environmental issues.<br>
				</b><br>
				<b>The whole world is talking about going green to save the 
				environment. What does ‘going green’ mean to you?<br>
				</b>Going green simply reflects our ability to conserve our 
				natural resources and become more environment friendly or to 
				reduce emission of toxic gases to save the ozone layer and 
				create a healthy breathing atmosphere.<br>
				<br>
				<b>How real is the threat of environmental degradation to planet 
				earth?<br>
				</b>The present “climate change” is a real indication that the 
				planet’s resources have been exhausted by man to enhance their 
				lifestyle. Also, there is a high consumption of oil products and 
				high emissions of sulphite, CO2 and non- degraded bi-products. I 
				believe in a balance between sustaining a healthy and 
				comfortable life with less damage to mother earth.<br>
				<br>
				Economic experts have suggested that creating a green economy 
				will top the agenda of most corporates by the turn of this 
				decade. Your comments.<br>
				<br>
				It is true. We need to invest more in going green in order to 
				keep a control on environment degradation. However, this will 
				create extra load on the finances of a company and this may be 
				passed on to the end users in terms of higher costs for 
				products. This means we will need to adjust our lifestyles to 
				costly products, which will make our living suitable for our 
				environment.<br>
				<br>
				<b>How is Omanoil contributing to the cause of environment 
				protection?<br>
				</b>We at Omanoil Marketing have adopted serious measures to be 
				safe and environment friendly by creating a culture in the 
				company, which educates individuals and gets them committed to 
				become friendly to Mother Earth.<br>
				<br>
				The major risk exposure of leakage and contamination due to 
				corrosion has been minimised by changing over to double skin 
				fibre-glass tanks manufactured to international standards.<br>
				<br>
				Omanoil strongly supported the government’s decision to phase 
				out leaded gasoline and this was achieved with no disruption to 
				customers. We also promote low sulphur diesel oil, which is 
				distributed both through Omanoil’s network of filling stations 
				and commercial installations.<br>
				<br>
				The company has successfully initiated new technologies in 
				environmental clean-up such as the redemption system which has 
				been designed, developed and implemented for the first time in 
				Oman and the Gulf.<br>
				<br>
				Further, we have made paper recycling as the main goal to be 
				achieved in 2010 both in-house as well as at our convenience 
				stores. Omanoil has received the “BP Top Honours” in upholding 
				the highest Health, Safety and Environment (HSE) standards in 
				Middle East and North Africa (MENA).<br>
				<br>
