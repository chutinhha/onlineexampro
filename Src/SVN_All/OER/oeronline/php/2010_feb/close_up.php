                <p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr">
				<b><font face="Verdana" size="4"><br>
                    </font><font face="Verdana" size="2">Mega spending 
				plans in regional economies<br>
				</font></b><font face="Verdana" size="1"><br>
				<b>All the GCC countries have plans for large-scale spending in 
				the next five years, thus providing a filip to different 
				economic sectors like construction</b><br>
				<br>
				<br>
				The mighty projected spending patterns of the Gulf Cooperation 
				Council (GCC) countries reflect welcome expansionary fiscal 
				policies, in turn serving the purpose of overcoming challenges 
				associated with the global financial crisis. Figures compiled by 
				Trowers &amp; Hamlins, the international law firm, point to sizeable 
				spending plans across the GCC.<br>
				<br>
				The UAE, in turn the second largest economy amongst GCC 
				countries after Saudi Arabia, stands out in terms of allocated 
				spending statistics. More specifically, the UAE is projected to 
				spend just over $1trn in the next five years on 2,674 projects. 
				Still, some $856bn of the projected amount is yet to be spent. 
				However, the UAE suffers from the phenomenon of delayed projects 
				in the aftermath of the global financial crisis. Also, it 
				remains to be seen to what extent Dubai’s debt restructuring 
				programme will affect implementation of construction projects in 
				the emirate.<br>
				<br>
				<b>Steady spending<br>
				</b>For its part, Saudi Arabia is geared to spend some $442bn, 
				of which $421bn remains to be spent. Yet a good portion of the 
				remaining amount is due to be spent in fiscal year 2010, thereby 
				confirming determination to press ahead.<br>
				<br>
				Projected Saudi expenditures for 2010 amount to $144bn, up by 14 
				per cent versus the originally planned figure for fiscal year 
				2009. What is more, the authorities allocated some $70bn for 
				developmental projects in 2010, up by $10bn from the previous 
				fiscal year. Undoubtedly, this is a considerable figure by 
				virtue of accounting for 49 per cent of total projected 
				expenditures. Projects envisaged include expansion of the road 
				network and new power and water plants, construction of numerous 
				educational and training institutes throughout the kingdom.<br>
				<br>
				Still, chances are that actual spending could end up being 
				higher than projected, much like fiscal year 2009. Revised 
				official estimated statistics for 2009 illustrate notable 
				changes from projected ones, with expenditures growing from 
				$126bn to $147bn. Also, total revenues are estimated to have 
				increased from $109bn to $135bn. As a result, the kingdom posted 
				deficit of $12bn, down from the $17bn projected figure. The 
				shortfall was the first of its kind since 2002 following years 
				of surpluses.<br>
				<br>
				<b>Smart housing schemes<br>
				</b>In addition, other GCC countries have their own sizeable 
				spending plans on numerous projects for the next few years. The 
				figures range from $166bn in Kuwait, $51bn in Qatar, $49bn in 
				Bahrain and $45bn in Oman. The Sultanate of Oman has plans to 
				spend some $16bn on airport infrastructure, tourism and heavy 
				industry.<br>
				<br>
				The same report by Trowers &amp; Hamlins indicates trends that 
				include a drive for affordable housing and moving away from 
				high-end residential projects in GCC economies. Recently, 
				Bahrain authorities opted for Chinese style smart housing 
				projects to meet demand. Many Bahrainis depend on the government 
				for their housing needs. Hundreds of Chinese workers were due to 
				arrive in Bahrain in early 2010 to help construct some 4,000 
				residential units. Selection of Chinese smart housing scheme is 
				designed to meet variables such as speed, cost <br>
				and efficiency.<br>
				<br>
				<b>Addressing challenges <br>
				</b>It can be argued that significant public spending is 
				essential for addressing economic challenges that include 
				creating jobs for locals. Already, two GCC states, namely 
				Bahrain and Saudi Arabia suffer from relatively high 
				unemployment rates amongst locals. Unemployment rates range from 
				eight per cent in Bahrain to 12 per cent in Saudi Arabia. For 
				instance, Saudi authorities are under pressure to create 160,000 
				jobs annually for nationals entering the job market. Demographic 
				statistics add to spending pressures, as about half of locals 
				are below the age of 20. Undoubtedly, many would be entering the 
				job markets in the next few years looking for suitable 
				employment.<br>
				<br>
				As argued, steady public sector spending is possibly more 
				important now than in the past few years if only to avoid 
				turning the financial crisis into an economic one. Already, some 
				reports point out to slower growth of gross domestic product 
				(GDP) in all regional countries. A report attributed to the 
				International Monetary Fund (IMF) estimated real (adjusted for 
				inflation) GDP growth in GCC states combined at five per cent in 
				2009 down from 7 per cent in 2008.<br>
				<br>
				<b>Sovereign wealth <br>
				</b>Indeed, the time is ripe for regional countries to use part 
				of their financial reserves to boost spending in local 
				economies. GCC countries enjoy extraordinary financial 
				capabilities. Latest available statistics suggest that together 
				GCC states have amassed some $1.5trn in the form of sovereign 
				wealth funds (SWF). The UAE stands out by virtue of accumulating 
				an extraordinary $875bn. Saudi Arabia, Kuwait and Qatar follow 
				suit with $300bn, $250bn and $40bn, respectively. GCC member 
				states succeeded in strengthening their SWFs over the last few 
				years on the back of firm oil prices.<br>
				<br>
				By the same token, accumulated surpluses pave the way for 
				stronger spending, in turn deemed essential to deal with 
				challenges of the global financial crisis. In the case of Saudi 
				Arabia, the budget posted a record $157bn surplus in 2008 on the 
				back of firm oil prices. Oil prices reached a record $147 per 
				barrel in July 2008.<br>
				<br>
				Therefore, the positive spillover effect and steady and strong 
				public sector spending serves the purpose of encouraging private 
				sector investors to follow suit, hence a welcome development. As 
				a rule, private sector investors draw comfort from stronger 
				government spending and see it as a measure of economic 
				direction.<br>
				<br>
				In short, much is at stake with regards to GCC countries in the 
				next few years. As indicated, Saudi Arabia, the largest regional 
				economy, intends to spend some $144bn in 2010, nearly half of 
				that on development projects. International firms need not miss 
				emerging opportunities in industrial, logistics and 
				infrastructure projects in GCC countries. Clearly, all roads are 
				leading to GCC economies.
