				<b><font face="Verdana" size="4"><br></font>
				<font face="Verdana" size="2">‘Products are no longer the 
                differentiator’</font></b><font face="Verdana" size="1"><br>
                <br>
                <b>David Power, the new general manager and chief retail banking 
                officer of National Bank of Oman, talks about his plans to 
                transform the retail offerings of the bank in a conversation 
                with Akshay Bhatnagar</b><br>
                <br>
                <b>What brings you to Oman?<br>
                <img border="0" src="images/banking01.jpg" align="right" hspace="8" width="200" height="181"></b>The 
                GCC is currently one of the best regions to be in at present, if 
                one is in the banking industry. Though the global recession has 
                hit a number of banks in the region, the local markets have 
                remained stable primarily due to significant government support 
                and healthy current account surpluses. Oman is indeed a very 
                good example of a structurally sound banking system with strong 
                support from the regulator. Prior to my current role in Oman, I 
                was based in Kuwait where I looked after the retail operations 
                of one of the leading banks in the country. <br>
                <br>
                When I was presented with the opportunity to move to Oman I was 
                extremely excited about the challenge and was particularly taken 
                by the history and beauty of the country! Over and above this, I 
                was also going to be working for the oldest and leading bank in 
                the Sultanate with the National Bank brand! Its value of “For 
                You. For our Nation” showed me its deep seated roots with the 
                community and its desire to become the best bank in Oman! The 
                bank had also embarked on a journey to reposition itself in the 
                community by doing a complete rebranding exercise as well as a 
                complete refurbishment of its retail franchise. This showed to 
                me the bank’s commitment not only to become better at what it 
                had done in the past, but it also showed me that it wanted to 
                remain connected to the lifeblood of the community no matter 
                what was changed! I have spent a large part of my life serving 
                various communities in my spare time and NBO’s commitment to 
                Oman and the Omani culture was a breath of fresh air in this 
                fast paced world that we currently live in. So for me, Oman 
                ticked all the boxes as it not only gave me a chance to serve 
                the community but also contribute to NBO’s quest to take its 
                retail banking services to newer and better heights.<br>
                <br>
                <b>How do you view the consumer banking scenario in Oman 
                vis-à-vis your experience in other markets?<br>
                </b>In the retail banking arena, Oman is not dissimilar from 
                rest of the GCC region with Dubai being the most likely 
                exception. Most of the local banks are in various stages of 
                modernization and all, without many exceptions, want to be 
                completive with the global players that are busy entering the 
                market. There is a desire to break away from the traditional 
                style of retail banking and move more towards a customer centric 
                model. There are however a number of challenges specifically 
                related to compliance, electronic banking, electronic channels 
                and the management of these areas. Having said this however I do 
                believe that retail banking in this region is rapidly catching 
                up with the rest of the world and is becoming more competitive 
                each year. The significant differentiator in this region is that 
                customer service is still a key factor in any retail banking 
                strategy. The other regions in the world have learnt at great 
                cost, that you lose focus in this area at your peril. It is 
                therefore quite interesting how so many global banks have now 
                made customer service the cornerstone of their respective 
                recovery strategies!<br>
                <br>
                <b>What has been the brief to you at NBO?<br>
                </b>To build on the rebranding exercise that was completed last 
                year and to drive NBO to be the most respected and highly 
                regarded retail franchise in the country. I have further been 
                tasked with expanding our revenue lines away from the 
                traditional net interest income model to one which is more 
                diversified. We also need to become the benchmark in customer 
                service and to do this we need to relook at a number of internal 
                aspects of the bank. We need to be constantly asking ourselves – 
                how do we make our customers’ life easier allowing them to be 
                more focused on their family and work environments. Banking 
                should be a pleasure and not a task and we intend to leave no 
                stone unturned to make this a reality! We have already begun 
                this journey with our award winning internet banking site. This 
                is ably supported by our recently launched 24x7 call centre, 
                full sms banking and so on. A number of new initiatives are 
                planned for roll out during the course of this year and it is 
                particularly pleasing to be doing all this during the 40th 
                Anniversary year of His Majesty benign rule.<br>
                <br>
                <b>Why should a customer place his savings in an account in NBO 
                hoping to strike it lucky in the Al Kanz scheme when his fund’s 
                purchasing power will reduce lying idle?<br>
                </b>It all depends on the customer’s risk profile. If the 
                customer has a higher risk appetite and invests in an investment 
                fund, there is a downside chance that he could lose some of his 
                capital. This has been evident over the past two years in the 
                global market. The old saying of high risk–high reward should 
                always be remembered when placing one’s hard earned cash into an 
                investment! If the customer is going to keep his money in a 
                savings account, his capital will always be guaranteed. What Al 
                Kanz 2010 does is to give our customer an opportunity to 
                significantly grow his capital and a chance to win a million 
                rials. Our intent is to continue to provide customers with the 
                opportunity to transform their lives and we believe that the 
                2010 scheme will reach out to an even wider cross section of the 
                community and continue to touch the lives of many. The new 
                savings scheme offers total prize money of 3.6 million rials 
                during the course of 12 months. The most unique feature of the 
                new Al Kanz Scheme is the popular loyalty programme. This 
                feature gives every Al Kanz customer more chances of winning for 
                every month they stay in the scheme. For example, a customer who 
                deposits 1,000 rials in month one will have 10 chances of 
                winning the monthly prize in that month, 20 in the next month, 
                30 in the next, going up to 60 chances for the December mega 
                prize.<br>
                <br>
                <b>What are your plans to enhance your customer service 
                standards?<br>
                </b>I don’t talk about customer service anymore, I talk about 
                customer delight. My team hears this saying regularly and we 
                intend to set the pace in this area! We are fortunate in that we 
                have very good processes and standards in place but our focus is 
                going to be on how can we enhance it further to ensure customer 
                delight. Other banks can offer similar products to us but they 
                can’t replicate our service standards. Products are no longer 
                the differentiator. <br>
