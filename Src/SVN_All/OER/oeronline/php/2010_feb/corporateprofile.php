				<font size="2" face="Verdana"><b>DOING ITSELF PROUD<br>
				</b></font><font face="Verdana" size="1"><br>
				<b>Oman Arab Bank is celebrating its 25th anniversary this year 
				and the bank has many reasons to be proud of, writes Visvas Paul 
				D Karra<br>
				</b><br>
				<img border="0" src="images/corporateprofile01.gif" width="250" height="194" align="right" hspace="9" vspace="5">If I drive from Muscat to Sohar, I feel very proud because many 
				of the roads and buildings that I pass by between the two cities 
				have been financed by Oman Arab Bank, says Abdul Kader Askalan, 
				CEO of Oman Arab Bank. Indeed, many of the infrastructure 
				projects in the Sultanate of Oman have been financed by Oman 
				Arab Bank (OAB). In Sohar, most of the big-ticket projects 
				contributing to the industrialisation of the nation have been 
				funded by OAB.<br>
				<br>
				That 
				is the powerful presence that has been established by Oman Arab 
				Bank, in Oman, since its inception on October 1, 1984. The bank 
				began its operations after acquiring the branches of Arab Bank, 
				which had been operating in Oman since 1973. Presently, Arab 
				Bank holds 49 per cent stake in OAB while the rest is held by 
				Oman International Development and Investment Co (OMINVEST).<br>
				<br>
				<b>Maximising maxim<br>
				</b>OAB provides a comprehensive range of services in retail, 
				corporate and investment banking. It has maintained its position 
				as a pioneer in financing projects in various sectors such as 
				infrastructure, industrial, petrochemical and tourism and 
				performed exceptionally well in the areas of trade finance and 
				corporate banking. The bank has also pioneered a number of 
				sophisticated products and services for its retail customers, 
				tailor made, depending upon their requirements.<br>
				<br>
				OAB derives strength from its parent bank, Arab Bank, which had 
				opened its first branch in Muscat, in 1973. “To understand the 
				legacy of OAB, it is interesting to note that Arab Bank had 
				financed the first industrial projects to come up in Oman, 
				namely Raysut Cement and Oman Cement Company to the tune of 50mn 
				rials and 40mn rials respectively. The third project, Oman Flour 
				Mills, was also funded by Arab Bank,” says Askalan, while 
				recollecting the bank’s initial days.<br>
				<br>
				Askalan recounts the routed history of OAB from its humble 
				beginnings as Arab Bank Plc to its present status as one of the 
				only banks in Oman to produce consistent results, year-on-year. 
				Indeed, while celebrating the silver jubilee of OAB, Askalan has 
				yet another reason to be proud; as he is the longest serving 
				banker in Oman.<br>
				<br>
				<b>Pioneer banker<br>
				</b>
				<img border="0" src="images/corporateprofile.gif" width="250" height="194" align="right" hspace="9" vspace="5">Askalan had actually come to Oman in 1973, for three days, 
				to visit the Muscat branch of Arab Bank. He saw a bleak horizon 
				but his keen business insight foresaw immense business 
				opportunities for the bank to grow and decided to stay back. The 
				rest of the story as to how he led the shaping and growth of the 
				bank, from being just a branch of an international bank to its 
				present form as a formidable local bank, is practically a case 
				study in building a financial institution.<br>
				<br>
				OAB’s growth and success can largely be attributed to its 
				conservative philosophy instilled into the bank’s core values. 
				Since its inception, OAB has followed the principles of business 
				conservatism of the Arab Bank, with a particular emphasis on 
				providing the highest level of personalised services to its 
				customers. <br>
				<br>
				The bank continues to actively participate in project finance 
				and some of the major, more recent projects financed, include 
				Sur IWP, Barka IWPP phase II; Oman Shipping Company; Oman 
				Aromatics and Sohar Aluminium. This is in addition to the 
				facilities granted to the EPC contractors of these projects. OAB 
				has also provided facilities to Sohar Ports’s new jetty 
				development and the EPC contractors to the wastewater project as 
				well as many other international and local companies.<br>
				<br>
				<b>Developmental role<br>
				</b>
				<img border="0" src="images/corporateprofile02.gif" width="250" height="186" align="right" hspace="9" vspace="5">Talking about the bank’s conservative policy, Askalan says, 
				“We continue to concentrate on project finance, be it 
				industrial, petrochemical, infrastructure or trade finance 
				related. In addition, we minimise our exposure to personal 
				finance. Our policy stems from adopting prudent, lending 
				practices that do not overburden our customers.”<br>
				<br>
				Emphasising his point, Askalan continues, “Some of the banks are 
				giving large personal loans up to 70 times and more of an 
				employee’s salary. How long should such people continue to be in 
				debt? Moreover, this will create many social problems.”<br>
				<br>
				Operating under this conservative policy instilled by Askalan, 
				the bank’s foreign exposure was nil thus helping the bank to 
				endure the global financial crisis, which took a heavy toll on 
				banks across the world.<br>
				<br>
				<b>Retail reach<br>
				</b>If Askalan is an old-school banker who has steered OAB 
				through the years with his keen business sense and instinctive 
				forays to build the portfolio of the bank, then Rashad Al Shaikh, 
				head - Retail Marketing and Business Development represents the 
				other side of the same coin.<br>
				<br>
				Young and full of enthusiasm, Al Shaikh is seamlessly merging 
				his aggressive approach to grow the bank’s business with its 
				core values by saying that OAB’s strategy is to rationalise and 
				leverage on the existing growth of the last few years under the 
				conservative umbrella. “We are developing our retail segment in 
				line with market needs. However, we do not believe in 
				overburdening our customer; as it is our CEO’s vision to curtail 
				this unhealthy trend,” says Al Shaikh.<br>
				<br>
				The bank’s goal is to develop products and services that are 
				need-based for the retail customer, under a conservative lending 
				policy umbrella. Under Al Shaikh’s guidance, the retail banking 
				division of OAB is also looking at various developments aimed at 
				enhancing and integrating the touch points with the customer.<br>
				<br>
				<b>Service channels<br>
				</b>“One of our main goals is to seamlessly integrate all 
				customer touch points or “fulfilment centres” in order to 
				provide our customers with high levels of personalised service 
				that connects emotionally with our clients,” says Al Shaikh. 
				Initially established with a share capital of 6mn rials, OAB’s 
				capital has increased progressively over the years and presently 
				rests at 75mn rials. OAB currently operates through a network of 
				53 branches and offices and a staff headcount of more than 800. 
				OAB also has access to Arab Bank’s large network of over 450 
				international branches in over 40 countries.<br>
				<br>
				The total assets of the bank as on September 30, 2009 were 771mn 
				rials. Moody’s Investors Service has upgraded the long term and 
				short term foreign currency deposit rating of OAB to A2/Prime-1 
				from A3/Prime-2. The outlook on these ratings has been changed 
				from positive to stable; showing the solid stability of OAB.<br>
				<br>
				The bank is currently building a new state of the art 
				headquarters in Azaiba, near the Ministry of Tourism office, to 
				accommodate the growing requirements of the bank and to be in 
				line with overall developments. Given its history, one can 
				confidently look forward to OAB financing many more buildings 
				and projects along multiple roads in Oman for <br>
				years to come. 
