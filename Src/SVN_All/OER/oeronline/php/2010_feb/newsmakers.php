				<img border="1" src="images/newsmakers.gif" width="200" height="262" align="right" hspace="9" vspace="5" style="border: 1px solid #B57C68">Stephen R Thomas, Renaissance Services CEO, has been given 
				recognition for his work in Oman that has spanned over 22 years 
				and countless responsibilities. Her Majesty Queen Elizabeth II 
				has graciously awarded Thomas an OBE (Officer of the Most 
				Excellent Order of the British Empire) in the UK New Year’s 
				Honours List published on December 31, 2009. The OBE medal is 
				awarded for a national order of merit and valuable service is 
				the sole criteria for the award.<br>
				<br>
				Thomas 
				has been instrumental in the development and progress of Oman’s 
				business and public community, as well as a fundamental leader 
				in the direction and growth of Renaissance Services, for the 
				past two decades. Thomas joined the Tawoos Group as general 
				manager of TISCO in 1988 and was appointed CEO of Renaissance 
				Services in 1998. He has also sat on the Boards of National 
				Hospitality Institute SAOG, Renaissance Hospitality Services 
				SAOG and Topaz Energy and Marine and Oman Society for Petroleum 
				Services (OPAL). He also helped found OPAL in 2001, Oman’s first 
				society for oil and gas operators, that promotes Omanisation, 
				quality, HSE and operational excellence in the industry. 
				Similarly, Thomas holds integrity and moral values in the 
				highest regard, and has consistently championed Omanisation and 
				education across industrial sectors. Beyond a long list of 
				contributions to the business community, he is also involved 
				actively in various community service including theatre and 
				educational charity organisations.<br>
				<br>
				Thomas was quick to explain that the award was the result of 
				years of support from colleagues and his family. “I feel very 
				humbled rather than aggrandised by it. Lots of people, from my 
				wife and family, my work colleagues, my friends, people from the 
				commercial world through to OPAL and the theatre group I am part 
				of have contributed to this honour. I hope that this honour is a 
				positive reflection on our whole community and the Omani British 
				relationship.” He also thanked Oman for presenting him 
				opportunities worthy of recognition in the UK honours list, “I 
				work for an Omani public company and the main body of my career 
				has been in Oman, so thanks must go to Oman for the 
				opportunities it has given me. I am very proud of Oman and thank 
				it for what I have done in my commercial life and personal life. 
				I am also very pleased that the UK honour system recognises 
				people here. It happens every now and then, as a few people in 
				Oman over the years have been honoured. It is great when it 
				happens here in Oman.”<br>
