			new company with the combined strengths of Hanover Compressor 
			Company and Universal Compression Holdings, Inc., Exterran is a 
			premier provider of sales, operations, maintenance, fabrication, 
			service and equipment for oil and gas production, processing and 
			transportation applications. With operations in every major 
			hydrocarbon region around the globe, Exterran offers customers a 
			world of solutions across the energy spectrum – from producers to 
			transporters to processors to storage owners.<br>
			<br>
			In Oman, the company was first created in 2005, under the name of 
			Hanover Middle East. The first project they undertook was the 
			Block-44 surface facility for PTTEP. “We provided the complete 
			production station for the Thailand oil company, which has a 
			production share agreement with the Ministry of Oil and Gas,” says 
			Pedro Diaz, regional business development manager, Middle East, 
			Exterran. This first job was completed in 2007 and was soon followed 
			by two more projects. One was the Daleel Gas plant in 2008; it was 
			used to recover natural gas from Flare, basically a flare reduction 
			project used to produce LPG and hydrocarbon condensate to be 
			produced with oil stream. The other project was compression at the 
			Zauliyah station, for PDO, in South Oman. One of the recent projects 
			taken up by Exterran Middle East was the provision of compression 
			services to Occidental Oman to support the Omani gas balance and 
			recover this gas from the flare.<br>
			<br>
			Through its contract operations business line, Exterran owns a fleet 
			of natural gas compression equipment and crude oil and natural gas 
			production and processing equipment that it uses to provide 
			operations services to its customers. Basically, Exterran is a 
			service provider to the oil and gas sector that provides the 
			manpower and the equipment to resolve the needs of the clients. So 
			if a client doesn’t want to grow in infrastructure or doesn’t want 
			to invest money on the surface equipment, Exterran can provide, 
			operate and maintain it for the client. In cases where the client 
			wants to buy the equipment, Exterran provides that service as well. 
			Exterran is also able to provide, not only gas handling and 
			producing facilities, but also crude oil early production facility 
			installations. <br>
			<br>
			The primary reason for their success in the region is the fact that 
			they own manufacturing facilities for processing equipment in 
			various locations; in Dubai and Jebel Ali Free Zone and also 
			manufacturing shops in the Hamriyah Free Zone. Including these they 
			have operations in 30 countries, manufacturing and facilities in 14 
			countries and four major engineering hubs. “When we say manufacture 
			we mean that in certain locations we can package compressors, in 
			others we can manufacture vessels, and in certain facilities we can 
			even recondition existing units,” adds Diaz. <br>
			<br>
			Considering that contract operations services comprises a large part 
			of Exterran’s business, this is extremely useful. Once a contract is 
			over, the equipment can be taken to be reconditioned and then put 
			back into the fleet. He adds, “We keep a fleet of production 
			equipment and compression that is available for the oil and gas 
			field as per their requirement. We own around six million horse 
			power worth of equipment which is available in different size 
			ranges.” <br>
			<br>
			Exterran is a company that strongly supports Omanisation and tends 
			to hire local personnel as much as possible, primarily young 
			engineers and technicians from the local universities and technology 
			institutes. During the initial phase of operations technicians are 
			brought in from various locations to train local personnel for six 
			months to a year, depending on the complexity of the project, and 
			then leave it in the hands of the local personnel. Exterran Middle 
			East the Oman branch of the company has around 120 staff of which 80 
			per cent is local talent, 20 are based in Muscat and the remaining 
			are in the field. <br>
			<br>
			Presently Exterran doesn’t have a fabrication shop facility in Oman 
			but if business continues to grow the way it has been over the past 
			four years, we can expect to see one in 2011 or maybe even late 
			2010. The facility, once ready, will not only provide additional 
			local jobs, but will also provide services to Exterran and also 
			other operators and producers in Oman.
