				</b><br>
                    <img border="0" src="images/inthenews01.gif" width="230" height="154" align="left" hspace="9" vspace="5">In 
				a major move to enhance services at the Port of Sohar, the Shell 
				Oman Marketing Company and Sohar Industrial Port Company have 
				inked an agreement to set up the port’s first bunker operation 
				for marine fuel The new service by Shell Oman is seen as an 
				added strategic advantage for the Port of Sohar, which is 
				located close to some of the busiest shipping lanes in the 
				world. The port is fast developing into a world-class logistics 
				points. Shell Oman will be working in association with Shell 
				Global Marine Products, with the objectives to providing 
				world-class bunkering services at Port of Sohar. <br>
				<br>
				Faisal al Hashar, managing director, Shell Oman said, “Providing 
				bunkering services in Sohar Port will create competitive 
				advantages for Shell Oman and it will enable us to extend our 
				excellent products and services to our local and international 
				marine customers who visit Sohar Port.” Mike Lumley, Shell 
				marine products regional general manager for the Middle East, 
				Africa and South Asia also added, “We are delighted to have been 
				granted this licence which will extend our network of ports in 
				the region in line with our global strategy of selective 
				expansion in core growth markets.” Shell Oman has stated that 
				besides the supply of high quality products, the company will 
				also put in place a robust HSSE (Health, Safety, Security, and 
				Environment) management programme.<br>
