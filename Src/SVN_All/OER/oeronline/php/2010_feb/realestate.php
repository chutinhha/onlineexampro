				<font face="Verdana" size="1"><br>
				</font><b><font size="2" face="Verdana">Reorienting property 
				needs for the coming decade<br>
				</font></b><font face="Verdana" size="1"><br>
				<b>The property world has changed – in all probability it won’t 
				be the same again after the economic crisis of 2008/09. It is 
				time to redefine the objectives and reorient strategies if we 
				want to maximise the potential of the real estate sector in 
				Oman, says Nick Smith, real estate and property business advisor</b><br>
				<br>
				<br>
				<img border="0" src="images/realestate01.gif" align="right" hspace="8" width="200" height="185">Before we look at expectations for the next decade 2010-2019, 
				let’s look at the headlines from the last one in the real estate 
				sector. The residential segment has changed drastically during 
				the last ten years, especially after 2006 with the change in the 
				freehold ownership law. It has created new opportunities. As a 
				result, we currently have three Integrated Tourism Complexes (ITCs) 
				– The Wave, Muscat; Muscat Hills; and Shangri-La’s Barr Al 
				Jissah Residency – which are either partly complete or expected 
				to deliver residential property shortly. Muriya are also 
				building at Sifa and Salalah. Some of the other ITCs have been 
				either put on hold or plans are progressing but not at the same 
				pace as per the original plan.<br>
				<br>
				<b>Relook at ITCs<br>
				</b>I think some analysis needs to be done on the progress of 
				the ITCs that were set up to bring in foreign investment and 
				promote tourism in the Sultanate. To some extent, ITCs have 
				worked, but they have not fully performed in the way they were 
				originally conceived. I’m sure the authorities will be looking 
				at how ITCs have fared so far, whether or not they are meeting 
				their desired goals, financially, socially and for tourism.<br>
                <br>
				All the ITCs have failed to provide affordable housing for the 
				Omani and GCC population. It can always be argued that they were 
				never set up to do that. They were more focused on bringing 
				investors from abroad. But, the question is, following the 
				global crisis, is there enough demand from overseas? With the 
				collapse of Lehman Brothers and the chain reaction that followed 
				that event, global dynamics have altered dramatically and the 
				world has become a very competitive place. We must also factor 
				in that there are many other countries that are also building 
				competing tourism schemes. Oman is one of the more expensive 
				places world-wide, and this will limit incoming investment. 
				Looking at the current situation, it will be quite a challenge 
				to attract a large number of foreign buyers (as originally 
				envisaged) to ITCs in Oman. The ITCs will have to relook at 
				their objectives and see how things could be done in a better 
				and more investment attractive way.<br>
				<br>
				<b>The Blue City<br>
				</b>The case of the Blue City is different from the other ITCs. 
				It is a huge project and the idea behind it was to be more like 
				a real city, with all of the differing land uses and functions 
				that a city provides. I believe the other ITCs are fundamentally 
				more tourism-oriented. The key to the success of Blue City is 
				land usage. All the necessary facilities such as schools, 
				hospitals, local government, religion, culture, transportation, 
				and recreation infrastructure are needed to create a proper 
				sustainable city. The key however is to create opportunities for 
				business. Without employment a city cannot grow, and job 
				creation, whether privately or publically initiated, is 
				fundamental. Without this, there is no reason for the people to 
				move in. Construction work at Blue City is going well, and it 
				has a role in the Asian Beach Games, which will give it a big 
				boost too.<br>
				<br>
				<b>Opportunities for growth<br>
				</b>The affordable housing sector is a very interesting and 
				highly promising area. It has been largely undeveloped in Oman. 
				There is a sizeable young population in the Sultanate. They are 
				seeking employment, many of them are moving from junior and 
				middle management to higher levels. They want good accommodation 
				to be built for them. As Muscat is growing, there is a huge 
				demand for affordable residential products to attract the 
				masses.<br>
				<br>
				The popularity of the affordable segment will be the single 
				biggest change in the next decade in terms of the housing stock. 
				There will be number of developers that will build better 
				quality well designed houses which are affordable also. Some 
				developers such as Zain and Abraj have already started doing it. 
				I think the banks will initiate more products in home loans and 
				financing to enable young people to own their residence by 
				starting with a small down payment and rest of the payment via 
				equated monthly instalments (EMIs) over a period of time.<br>
				<br>
				The traditional values in Oman are to aspire for large villas 
				which ensure privacy behind high walls. This will remain a 
				priority for the upper class whereas the entry level will go for 
				smaller houses out of necessity but they can still be designed 
				to offer privacy and reflect culture.<br>
				<br>
				In terms of location, when I look around, I see growth happening 
				in the areas around the airport. The west of the airport will 
				grow dramatically in the next decade. Beyond Muscat, Sohar is 
				the obvious next location. With the new coastal road, it will 
				change significantly. People have a real reason to go there as 
				jobs are being created.<br>
				<br>
				The Blue City is another interesting prospect as it is on the 
				coast between Muscat and Sohar. As mentioned above, I believe 
				the Blue City has to become more flexible with the changing 
				times and become a proper city, and I think it is going down 
				this route already. Affordable housing for Omanis is a big 
				opportunity there. The government may also have to look at 
				easing regulations, to proactively bring in business and 
				residents, to help it achieve the role of a big city. Without 
				these positive measures it is difficult to imagine it reaching 
				its target of 250,000 inhabitants. Their first hotel is expected 
				to be ready by the Asian Beach Games, and activity will then be 
				openly visible.<br>
				Everywhere, the government must continue to kick start new 
				cities and towns. Duqm, I believe, will definitely happen on the 
				back of the port and airport. In 10 years, it will be a 
				different place, but it is more of a 25 year plan. And Salalah 
				will grow, predicated by the port and expanded air traffic.<br>
				<br>
				<b>Who will drive investment in Oman’s real estate sector?<br>
				</b>This is a difficult question to answer at this stage. With 
				the exception of the Blue City, most of the other ITCs in Oman 
				are funded by financing from the region. The rest of the world 
				is recovering from recession at a very slow pace. The region is 
				in a better position than most, but Dubai’s woes are going to 
				make inflow of investors to the region and Oman a lot slower, 
				and they will be more cautious about making commitment.<br>
				<br>
				The good thing about Oman is that the Sultanate has followed a 
				very controlled approach towards development and with time this 
				has proven successful and more prudent than some other 
				economies. But the next three-to-five years are going to be 
				challenging. I don’t foresee a drastic leap in Oman’s real 
				estate landscape. Currently things do not happen that fast here. 
				There are notable opportunities for more leisure and retail 
				oriented developments, on and near the beaches, promenades and 
				in places where people want to spend their leisure time. There 
				are very few of these places in Oman.<br>
				<br>
				In terms of office space, there is an oversupply right now in 
				Muscat. A couple of years ago, under supply was an issue. But 
				the market will correct itself. There will be a gradual increase 
				in demand and fresh supply will not come on stream until then.<br>
				<br>
				As a final thought, I suggest that if various government 
				ministries and authorities could work more cohesively to fast 
				track critical projects and issues through single window 
				systems, it could go a long way in optimising the full potential 
				of the real estate sector in Oman. This would help both 
				government and private initiatives. Making the entry into the 
				market easy for investment is the major key to stimulating the 
				property sector. <br>
				As told to Akshay Bhatnagar. <br>
