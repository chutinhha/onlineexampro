                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The human factor</font><p><font face="Verdana" size="1"><b>In a recently released report on human development in Arab countries, UNDP stresses that although a considerable amount of socio-economic development has taken place, there is a vital need to increase the participation of the local people for developmental purpose. OER takes a look at the report</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_august/the-human-factor.jpg" align="left" hspace="5" vspace="5" width="160" height="212"></font><font face="Verdana" size="2">Rapid technological advancements in the last few decades have undoubtedly left the entire world transformed. While most of these advancements are directly linked to openness, liberalisation and financial synergy, and to a trend among nations to merge and create economic blocks, the role of technology in value addition has become crucial. But central to these enormous developments are the capabilities of people. Meaning all the progress starts with human development, and this in turn emphasises the importance of enhancing the people's creative talents as fundamental factors in the progress of any nation.<br>
                    <br>
                    Aforesaid are a few pertinent points stressed in the recently released 'Arab Human Development Report 2002' by United Nations Development Programme (UNDP). Among other things, the report highlights the substantial progress made by the Arab countries in the area of human development, whilst outlining the need for strengthening personal freedom and boosting broad-based citizen participation in political and economic affairs. The report places people at the centre of development and provides a forum to measure progress and deficits whilst proposing strategies to policymakers.&nbsp;<br>
                    <br>
                    According to the report, although Arab nations have seen a remarkable economic and social growth during the past three decades, the advent of the 21st century has brought complex economic and social problems that stand to affect their present and future. "Problems like illiteracy, slowdown of scientific research and technological development, poor production bases and competitive capacity and unemployment need to be tackled quickly," says the report which covers a total of 22 countries from Maghreb to the Gulf.&nbsp;<br>
                    <br>
                    It further states: "All these conditions compel the Arab countries to adopt a balanced package of reforms so as to promote the well being of the community at large. This can be realised through reconsidering the content and goals of education and scientific research; developing institutional structures and cadres; enhancing production and services; and promoting joint and integrated initiatives among the Arab countries."<br>
                    <br>
                    In a nutshell, the report covers critical challenges in health, education, science and technology and the environment, as well as prospects for job creation and Arab cooperation, highlighting development and progress in all these fields. It further sheds light on the provision of opportunities and incentives for coming generations. In doing so, it looks at reforming education, stimulating research and development, securing appropriate health and environmental requirements, revitalising economic growth, creating suitable mechanisms for the fair distribution of wealth, developing and activating institutional structures to manage the economy and promote civil society.<br>
                    <br>
                    <b>Key observations</b><br>
                    The report's key messages are simple and compelling. There has been considerable progress in laying the foundations for health, education and housing, with two notable achievements being the expansion in educating the youth and improvement in fighting death. Life expectancy, for instance, has increased by 15 years over the last three decades, and infant mortality rates have dropped by two thirds. Moreover, the region has much less dire poverty than any other developing region. Adult literacy has almost doubled and women's literacy has trebled reflecting very large increases in gross educational enrolments, including those of girls.&nbsp;<br>
                    <br>
                    But alarming signals do exist for the Arab region which over the past 20 years has reported the lowest growth in per capita income in the world. "At a growth rate of 0.5 per cent annually, if such trends continue in the future, it will take an average Arab citizen 140 years to double his or her income, while other regions are set to achieve that level in the next 10 years," the report clarifies.<br>
                    <br>
                    The report also makes it clear how much still needs to be done to provide current and future generations with the political voice, social choices and economic opportunities they need to build a better future for themselves and their families. Improvements in health and education are yet to reach all citizens. The report also underlines how far the Arab states still need to go in order to join the global information society and economy as full partners.&nbsp;<br>
                    <br>
                    "Although the countries constituting the Arab region vary significantly, in area, in population size, in physical and ecological features, and in levels of human well being, they are unified by unbreakable ties. Foremost among these is a common language and a vibrant cultural heritage that today still lives potently in a common cultural context that unites these peoples," states the report.<br>
                    <br>
                    Consequently, the Arab Fund for Economic and Social Development gives due attention to the broader developmental priorities of Arab countries. The Fund has financed a wide range of social development projects and programmes. These have included rural development projects, credit schemes, social safety nets for the poor and unemployed, as well as development management, training and institutional support programmes.<br>
                    <br>
                    <b>Development facts</b><br>
                    Achievements by the Arab region on the Human Development Index (HDI) in the past decade were lower than the world average. Relative to other regions, the Arab world does better on income indicators than on development indicators, which means that the region is richer than it is developed. "Although income poverty is low compared to other parts of the world, the region is hobbled by a lack of opportunities. The way forward involves tackling human capabilities and knowledge," says the report.&nbsp;<br>
                    <br>
                    Labour productivity has been low and is declining. In fact, total productivity declined at an annual average of 0.2 per cent during 1960-90, while it rapidly accelerated in other parts of the world. The productivity of Arab industrial labour in 1960 was 32 per cent and had fallen to 19 per cent by 1990. The decline in workers' productivity has been accompanied by a downfall in real wages. It is estimated that one out of every five Arabs lives on less than US$2 per day. Open unemployment in the Arab region was estimated to be no less than 12 million in 1995, around 15 per cent of the labour force. If present rates continue, the number is expected to rise to 25 million in 2010.<br>
                    <br>
                    The combined population of the 22 Arab countries covered by the report was 280 million in 2000, five per cent of the world. Arabs are younger than the global average, 38 per cent are aged between 0-14. Migration within, from and to the Arab region is an important demographic feature, as is urbanisation, since half the population lives in cities compared to a quarter in 1950. Growth rates are still high, and there will be between 410 and 459 million Arabs in 2020, with a slightly older age structure than that of today. The new demographic profile will present both challenges and opportunities for Arab countries.<br>
                    <br>
                    The digital gap between Arab countries and the advanced world has also widened. Cyberspace is becoming a crowded place and powerful players are taking up much of the room. There are also information disparities between Arab states, even when levels of human development are similar. In addition, there are factors widening the digital gap within each Arab country, with language being the decisive factor.<br>
                    <br>
                    <b>The way forward</b><br>
                    The resources for development exist in the Arab region. The question is: where should they be placed to maximum effect? According to the 1996 Human Development Report, every country that sustained both human development and economic growth accelerated advances in human development first, or pursued both objectives simultaneously.<br>
                    <br>
                    In the Arab region, three areas stand out as investment priorities - building Arab capabilities and knowledge; using human capabilities through re-invigorating growth and productivity; and promoting good governance. A key driver of change would be a transformation in attitudes and societal incentives to support greater creativity and innovation. The rules of the social game need to be modified to provide incentives for embracing change and to afford real opportunities for social recognition, renewal and advancement to those who work in areas of knowledge.&nbsp;</font></p>
