                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Roll out the barrel</font><p><font face="Verdana" size="1"><b>Bears may be running amok on the world's equity trading floors, but bulls are very much on the forefront across the corridor on oil desks. Share values have nose dived since the beginning of the year, but oil prices have shot up - and many seasoned observers now believe they have further to go, says Richard Dean</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_august/lng-oil-&-gas.jpg" align="left" hspace="5" vspace="5"></font><font face="Verdana" size="2">William Buchanan and Don Jameson, energy analysts at Standard Bank in London, have marvelled at the eccentric movements of both stock and oil markets since 2002 began. "It is interesting to note that the depredations of America's kleptocratic CEOs have now given deeper wounds on the Dow Jones index than those inflicted by Al Qaeda last September," they wrote in their July 15 weekly energy market commentary. "By contrast, the crude markets have shrugged off Wall Street's misery and ended the week on a positive note."<br>
                    <br>
                    Dated Brent traded around US$26 per barrel in late July, up from a trough of US$17 per barrel in January. Over the same period, the Dow Jones index fell from some 10,000 points to near 8,000. It's a far cry from 1999, when the technology-fuelled NASDAQ (which has fared even worse than the Dow Jones in recent months) was on a seemingly endless upward spiral, while oil fetched barely US$10 a barrel.&nbsp;<br>
                    <br>
                    "There was anecdotal evidence towards the end of the week that hedge funds were actively buying September IPE Brent futures in a shift from UK equities," says the Standard Bank report. The trend was mirrored in the US - and the irony was not lost on Buchanan and Jameson. "It is a curious state of affairs when a basic commodity, mired in surplus, driven by a fractious cartel based largely in one of the least stable geopolitical areas in the world, is perceived as a safer haven for investors than stocks and bonds in the strongest national economy on the planet."<br>
                    <br>
                    Two questions spring immediately from this analysis. What has driven oil markets in recent months and, most importantly, whether the bull run is sustainable? The first is, predictably, the easiest to answer. Oil prices have been supported by three forces. First, the global economy recovered much quicker than anticipated, particularly in the US, the world's main energy consumer, boosting oil demand. Second, OPEC has slashed output by some five million barrels per day (bpd) since early 2001, further strengthening the oil supply/demand price formula.<br>
                    <br>
                    Finally, political tension in the Middle East has led to a 'risk premium' of between US$3 per barrel to US$6 on each barrel of crude. Traders have been worried that Israel's military campaign in the Palestinian territories, or the proposed US invasion of Iraq, will lead to a significant dip in supply, either through an Arab oil embargo or bombing of supply infrastructure during a military campaign.<br>
                    <br>
                    Predicting oil market movements is a far trickier business than analysing the reasons behind historic shifts. But most soothsayers agree that prospects for this year and next are good. "June marked the fourth consecutive month that the OPEC basket price averaged above US$22 per barrel, the lower end of OPEC's target range," says the US Government's Energy Information Administration (EIA) in its July Short Term Energy Outlook. "The basket price has been above US$22 per barrel since March 8, 2002 and is projected to remain within the target range (US$22-28 per barrel) through 2003." The EIA's base case projection for West Texas Intermediate (WTI) in 2002 is almost US$26 per barrel, rising to more than US$29 per barrel in 2003.<br>
                    <br>
                    A surge in oil demand will be the main driver, says the EIA. "With the expected recovery of the economy in 2003, particularly in the US, where GDP growth is projected to reach over three per cent annually, oil demand could increase by 1.3 million bpd in 2003."<br>
                    <br>
                    OPEC supply restraint is another key variable in the equation. And most members are happy with the current price, so any formal increase in supply is unlikely. This is despite the very real risk of losing market share to ambitious non-OPEC rivals such as Russia.<br>
                    <br>
                    Saudi oil minister Ali Al Naimi, OPEC's main power-broker, said in July that prices were at "the level where we don't need to make a change...the market is in a good balance." His comments were echoed by Iran's Oil Minister Bijan Namdar Zangeneh, signalling that he's moderating what once was a hawkish price stance. "We don't have to have exactly the higher range of the band," Zangeneh says. "It's a band, and US$22 is fine for us."<br>
                    <br>
                    As ever with oil price forecasts, several caveats are attached. Even Washington hasn't decided how and when it will attack Iraq, one of the world's major producers, so factoring in the impact on price is near-impossible (though most analysts predict little more than a short-term spike followed by a return to normality, given the excess capacity in the global system at present). At the other end of the spectrum, the possibility remains that OPEC will flood the market with cheap oil to teach non-OPEC rivals such as Russia, a lesson. But on balance, the big money is on oil prices staying where they are for the next 18 months. And that can only be good news for the Gulf.</font></p>
