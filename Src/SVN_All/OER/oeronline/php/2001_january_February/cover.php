                  <td width="100%"><img border="0" src="../../images/2001_january_February/img1.jpg" align="left" hspace="5" vspace="5" width="175" height="240"><font face="Verdana" size="4" color="#FF0000">Muscat Securities Market</font>
                    <p><font face="Verdana" size="2">CMA President His Excellency Yahya Bin Said Al Jabri talks about the delayed cross-listing agreement with the Bombay Stock Exchange, the implementation of the much-awaited insurance programme to protect against professional misconduct, and further mergers pending between brokerage companies following the government's efforts to boost the Muscat Securities Market in early-2001.<br>
                    <br>
                    With the announcement that only Gulf Investment Co. SAOG and Financial Services Co. SAOG are seeking approvals to merge, how successful has the plan to get brokerage companies in Oman to form alliances been?<br>
                    <br>
                    The number of brokerage companies in Oman is overly large for a market such as ours and so the government's incentive package proposed by the Economic Coordination Council - and approved by His Majesty Sultan Qaboos - is definitely going to improve the situation.<br>
                    <br>
                    Yes, we have heard of two companies who are intending to merge so far, but we know there are plenty of others in discussions. There is also a demand for the small private brokerage companies to merge and we will also give advice to these firms too although they do not have public shareholders. Ideally, we will see the number of publicly listed companies on the MSM merge and reduce from their current number of 10 down to five. Should they not decide to take advantage of the five-year tax holiday, 4.0% soft loan and the added advantage of dealing with the government's RO50 million fund, these brokers will still be required to raise their capital to RO5 million or leave the MSM."&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2001_january_February/img2.jpg" align="right" hspace="5" vspace="5" width="200" height="133">"Isn't government intervention in the markets perceived as a major sign of weakness and now that you have intervened once, are you likely to do so again?"<br>
                    "This is a once-only measure and investors should learn from this. We want brokerage companies to play the role of advisor in a proper manner. By merging, these companies will be able to take on this responsibility rather than continue as small outfits lacking the resources to do so. It is also important that brokerage companies play a good role in promoting the Muscat Securities Market. We feel that they let investors down and did not educate them well enough about potential investment losses.<br>
                    <br>
                    It has been reported that this was a 'rescue package' for the MSM. That would be a very simplistic analysis of what has been implemented. The measures are essentially about restoring faith and sorting out the difficulties experienced by investors and their trust funds as well as sorting out the issues of brokerage companies and the listed companies.<br>
                    Such government intervention is not repeatable and I can assure you that we will not do this kind of thing every now and then."&nbsp;<br>
                    <br>
                    "What do you define as 'good quality shares' in terms of the RO50 million fund that the government will buy in the MSM to improve liquidity and who will control their purchase?"<br>
                    "Firstly, the fund will only work with those brokerages that merge and is therefore another major incentive for this to happen. Secondly, the bad shares have already been stored and housed by the government in the form of the trust accounts. Thus, most of the deficit is due to shares that couldn't be sold so you could say that all the bad stock has left the market and only good shares remain on the trading floors. The brokerage companies are aware of this and will obviously want to make money themselves and so we expect them to buy the blue chip stocks listed on the MSM.&nbsp;<br>
                    <br>
                    However, the fund is not held with us. It is a special account funded from the State General Reserve Fund (SGRF) and the Investment Committee of this fund will decide which sectors to invest in and which shares to buy after consulting financial experts and the brokerage companies etcetera."<br>
                    <br>
                    "Do you think investor confidence has returned to the market following government's measures dspite some disappointing third quarter results being posted?"<br>
                    "A certain number of companies in the third quarter may be a little disappointed with their recent performances. However, there are also some companies that have done quite well and this is natural in any given market. The issue in the MSM is to make sure that it is transparent and that all companies disclose information on time to their shareholders so they can decide whether they want to continue to invest in that particularly company. This transparency is something that we have been bringing to the market and continue to monitor.&nbsp;<br>
                    <br>
                    Investor confidence is not only about the government's fund, solving the problems of the trust account or giving incentives for mergers to take place. Confidence is also about abiding to regulations and corporate governance. As you know we have de-listed some companies because they did not comply with market regulations. We have shown the market that we have some teeth, so to speak. So while we have used a stick to control the market, this package is a carrot. And I don't see any more excuses for any companies to violate rules in the future.&nbsp;<br>
                    <br>
                    Recently, we had a meeting with the Ministry of Commerce and officials from all the publicly listed companies to detail our intentions and listen to their comments. We have helped those with problems restructure and advised the promoters of companies with serious erosion of their market capitalisation that we would de-list them from the Secondary Market to the Parallel Market, then into the Third Market and, failing any change in performance, advise them to pay off their shareholders and convert into LLCs [limited liability companies]. If you lose over 75% of your market cap, then you're out of the MSM. Of course, some people will not be happy but at least the confidence will return to the market and ensure better company management in general. To be fair, we have seen a lot of companies restructure and begin to sort their problems."<br>
                    <br>
                    "Why has there been a delay to the insurance scheme to protect investors and companies against professional and criminal misconduct in the securities investment industry?"<br>
                    "We postponed this until a sufficient number of brokerage companies had merged. There wouldn't have been any point in these companies having to insure twice. Otherwise, this issue is still in hand and is still a priority which we should see happening at the beginning of 2001."<br>
                    <br>
                    "What about the cross-listing of bourses?"<br>
                    "There is an agreement between most Gulf stock exchanges about this but there is hardly any activity because normally the local people prefer to invest only in the companies that they know locally. Also, because of the variety of companies involved, it is essential that company laws and market regulations align across the Gulf bourses to enable easier performance comparisons and transfer of stock for the investor. It is something that the bourses are working hard to achieve and there are ongoing studies on this too. We also have had a long-standing agreement with the London Stock Exchange (LSE). We concluded a Memorandum of Cooperation in November 1999 and have since benefited from the expertise of the LSE. There is also an agreement being prepared for signing between the Bombay Stock Exchange and the MSM."<br>
                    "When will the agreement with the Bombay Stock Exchange be signed?"<br>
                    <br>
                    "We hope it will be sometime in January 2001. A visit from Bombay Stock Exchange officials was meant to take place in November but was postponed by them. But once we do meet, it will only be a matter of time before we sign an agreement on cross-listing of shares which will obviously benefit the large Indian expatriate investor base in the Sultanate also."&nbsp;<br>
                    <br>
                    "What other developments are planned in 2001?"<br>
                    "We are actually considering many things at the moment and are planning to lift the ceiling on daily trading limits on the MSM which currently stands at 10%. The CMA is looking to float commission rates rather than keep them at fixed levels in order to boost liquidity and share prices. We are studying bourses in the region that have limits up to 25% but there is no definite decision on what changes will be made and when they will come into effect as yet."<br>
                    <br>
                    <b>The government's measures to strengthen the MSM</b><br>
                    <br>
                    <b>Trust Accounts and small investors</b><br>
                    The government shall buy in all trust accounts with negative positions at the market price of these shares which have been estimated at RO14 million on November 12, 2000.&nbsp;<br>
                    <br>
                    The government shall bear 50% of the deficit (about RO24 million) brokerage firms 25% and banks will bear the remaining 25% which will be deducted from the broker's accounts at the banks.<br>
                    <br>
                    The government will keep the shares bought at a cost of RO14 million for five years during which time the investor has the option to buy back the shares from the government at the same purchase price.&nbsp;<br>
                    <br>
                    The CMA, in coordination with brokerage firms, will review each of the 617 trust accounts with negative positions in order to pick out small investor's accounts, identify the deficit thereof and prepare a report on them to the ECC for an appropriate decision.<br>
                    <br>
                    <b>Brokerage Companies</b><br>
                    For brokerage companies established as joint-stock companies which suffer deficit in their balance sheets either because of negative trust accounts or because of the situation prevailing at the MSM, the following has been decided:<br>
                    To merge these companies so that their number would come down to five, provided that the capital of each would not be less than RO5 million<br>
                    To encourage mergers among such companies, the government shall advance to merging companies a five-year soft loan to the extent of 50% of the capital of the company at a 4.0% interest rate; and will be eligible for a five-year profit tax holiday.<br>
                    <br>
                    Companies deciding not to merge shall raise their capital up to RO5 million and will not benefit from the support measure specified;<br>
                    <br>
                    For private brokerage companies (closed or limited liability companies and partnerships), the following has been decided;<br>
                    <br>
                    The capital of private brokerage companies shall not at any time be less than the limits prescribed by the new executive regulations of the Capital Market Authority Law.<br>
                    <br>
                    <b>Proposed measures for increasing liquidity at the MSM</b><br>
                    The government shall transfer RO50 million to a special investment account set up to purchase good quality shares in accordance with specific investment criteria through the newly established brokerage companies which satisfy the requirements of merger, capital adequacy and technical capabilities.<br>
                    <br>
                    The special account shall be funded from the State General Reserve Fund (SGRF).<br>
                    <br>
                    <b>Miscellaneous</b><br>
                    The Economic Co-ordination Council stresses the importance of strict regulatory controls prescribed by CMA laws and executive regulations including the application of penalties in order to limit violations, tighten surveillance, increase inspection on public joint-stock companies and inspire investors with confidence.<br>
                    <br>
                    The Capital Market Authority de-listed seven companies that lost most of their capital and transferred them to the Third Market (suspension of trading). These companies are as follows: Marina Bander Al-Rowdha Co., Sweets of Oman, Al Sallan Food Industries Co., Dhofar Marble and Granite, Majan Glass Co., Tmoor Oman, and Oman Filters Industry Co.<br>
                    <br>
                    The CMA also decided to suspend shares traded in Oman Securities Portfolio Co. and move its listing in the Third Market for the same reason.&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1">Source: Capital Market Authority November 12, 2000</font></p>
