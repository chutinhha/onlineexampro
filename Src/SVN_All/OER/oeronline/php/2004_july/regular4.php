            <p align="justify"><b><font face="Verdana" size="2" color="#FF0000">
            Class ap</font></b><font size="2" color="#FF0000" face="Verdana"><b>art</b></font><b><font face="Verdana" size="2" color="#FF0000"><br>
            </font></b><font face="Verdana" size="1"><br>
            <br>
            <b>The all-new Mercedes-Benz C-Class cars offer the Benz experience 
            at a reasonable price, which is why they seem all set to become the 
            company's best-selling line. Vicky Kapur test drives the new C230 
            Kompressor</b><br>
            <br>
            <img border="0" src="car.jpg" align="left" hspace="5" width="200" height="166">The 
            new generation Mercedes-Benz C-Class with the 'Direct Control' 
            package now offers an even more sporty driving experience thanks to 
            upgraded suspension and steering plus a high performance 
            top-of-the-range AMG version. The interior and the cockpit now has 
            sophisticated new controls and improved seating. The range of 
            C-Class engines has been extended with a 192 hp supercharged 
            powerplant. With a maximum torque of 260 Nm, this four-cylinder unit 
            accelerates the C230 Kompressor from zero to 100 km/h in 8.1 seconds 
            and provides a maximum speed of 240 km/h. <br>
            <br>
            For 2004, Mercedes has focused improvements on the sportier models. 
            The sport sedans get a healthy dose of performance and styling 
            updates, including a lower ride height, upgraded brakes, a 
            short-throw shifter and unique interior features to further 
            distinguish them from the standard sedans. The sport coupes benefit 
            from similar interior changes, and now come standard with 16-inch 
            alloy wheels and high-performance tyres. Cars in the Mercedes-Benz 
            C-Class start from an affordable RO10,995 onwards. <br>
            <br>
            The C230 Kompressor (RO13,245 onwards) is a pleasure to drive, with 
            its smooth, powerful engine, responsive automatic transmission and 
            classic Mercedes balance of ride quality and handling. Inside, it 
            looks and, for the most part, feels like a Mercedes-Benz, with firm, 
            supportive seats and high-quality materials. The C230 Kompressor 
            sedan is powered by the same supercharged inline-4 as the C230 
            sports coupe. <br>
            <br>
            <b>Exteriors</b> <br>
            The C-Class has good bones, with exterior styling inspired by the 
            big, luxurious S-Class, technology shared with the E-Class, and an 
            interior design all of its own. Every model, starting with the 
            less-expensive coupes, comes standard with a full-complement of 
            airbags and Mercedes' Electronic Stability Program skid-control 
            system. If you want Mercedes-Benz engineering, design strengths and 
            mystique in a solid, mainstream sedan, it doesn't get anymore 
            accessible than the C-Class.<br>
            <br>
            The C-Class cars are a mix of design cues from other Mercedes-Benz 
            cars. All C-Class models feature quad ovoid headlamp assemblies that 
            are joined, similar in design to that of the CLK and SL models. 
            C-Class sedans wear the horizontal-bar grille topped with the 
            traditional three-pointed star hood ornament, similar to that of the 
            S-Class and E-Class sedans, but the much smaller C-Class sedans 
            nonetheless have a tight look of their own. Aerodynamically, they're 
            among the slickest cars in the Mercedes-Benz family. <br>
            <br>
            The C-Class sports coupes have firmer suspensions, racy body 
            cladding, 16-inch spoke wheels with high performance tyres, the 
            six-speed manual transmission, thickly bolstered front sport seats 
            and wooden interior trim. Up front, the Mercedes star-grille is used 
            in place of the sedan's bar-grille, with the tri-star badge 
            ensconced in the grille work, similar to that of the SL, CLK, and CL 
            models. <br>
            <br>
            The coupe's double-elliptical headlights are similar to those of the 
            C-Class sedan. A rear spoiler adds aerodynamic downforce to the rear 
            tyres, good for high-speed curves when the road is wet, and a glass 
            panel between the tail lights contributes to rearward visibility and 
            gives it a distinctive appearance. All C-Class cars ride on the same 
            106.9-inch wheelbase. <br>
            <br>
            The more dynamic appearance of the C-Class is created by the 
            redesigned front bumper with its striking, sportscar-like lower air 
            intake and the likewise redesigned radiator grille. Even more superb 
            handling dynamics are ensured by various technical modifications to 
            the suspension, steering and manual transmission. This package of 
            measures, which is known as Direct Control, includes newly developed 
            front and rear axle bearings which provide more agile cornering with 
            no loss of comfort, a more direct steering ratio and reconfigured 
            standard and sports suspension systems. <br>
            <br>
            <b>Interiors</b> <br>
            The C-Class interiors look and largely feel like Mercedes-Benz 
            interiors. Their firm, supportive seats are supremely comfortable: 
            neither so hard that they numb the backside nor so soft that they 
            leave occupants tired or aching. The C-Class switchgear, again with 
            a few exceptions, is easy to use and understand. The lack of some of 
            the complicated systems present on more expensive Mercedes cars 
            simplifies things. The deeply hooded instrument cluster virtually 
            eliminates glare on the dials. The layout and décor of the centre 
            stack is very likeable, with its wood trim and large controls for 
            the audio system and climate controls. <br>
            <br>
            The stalk controls have a nice, beefy feel with positive detents. 
            Among them is a stalk for the cruise control. Many drivers new to 
            Mercedes cars, and that includes me, often move this cruise control 
            stalk when we intended to use the turn signals. This can cause the 
            cruise control to switch on right as you are about to brake for a 
            turn or make a lane change. Controls mounted on the steering wheel 
            allow the driver to operate the audio and telephone systems, and 
            other functions.<br>
            <br>
            Overhead is a console with nice map lights. The glove box is a good 
            size, unless you order the CD changer, in which case you lose most 
            of it although you'll still have the centre console and door 
            pockets. Still, we'd prefer if there were more places to stash 
            things around. <br>
            <br>
            The seats in the sedans come standard with cloth facings althoiugh 
            the C230 Kompressor that OER test drove had leather ones. In 
            general, the C-Class models are trimmed with good-quality interior 
            materials. The rear seats in the sedans are generous in space and 
            comfort. They're installed theatre style, and sit slightly higher 
            than those in front, allowing rear passengers a better view forward 
            without significantly compromising head room. Indeed, the spacious 
            rear seats in the sedans are one of the strengths of the C-Class 
            when compared to some competitors. The optional power sunshade for 
            the rear glass reinforces the feeling you're in a Benz. <br>
            <br>
            The sedan's trunk is spacious for a car of its compact exterior 
            dimensions. It offers 430 litres of cargo space. Moreover, when you 
            hit the trunk button, the C-Class lid pops all the way up, something 
            many trunk lids do not do. <br>
            <br>
            <b>Performance</b> <br>
            The Mercedes C-Class offers a fine balance of ride and handling, 
            nice steering and excellent brakes. We found the C230 Kompressor 
            smooth and quiet in normal driving, but step on it and the engine 
            growls to life. The 1.8-litre 4-cylinder in-line engine works great 
            in this car, with 260 Nm of torque available from 3500 to 4000 rpm. 
            Torque is that force that propels you away from intersections and 
            stop lights, and the C230 has a healthy supply for a car its size.<br>
            <br>
            The automatic transmission in the C230 works very well. The Touch 
            Shift automatic has a manual-shift feature, but there's no need to 
            use it because the automatic is so responsive. It's adaptive, which 
            means that if you drive it gently, it will upshift at lower rpm and 
            reward you with good gas mileage. The C230 Kompressor gives you a 
            100 km per 12.6 litres of fuel. <br>
            <br>
            If you're constantly on the throttle, the transmission learns that 
            you like to drive quickly and will hold itself in each gear longer 
            for quicker acceleration performance. Moving the Touch Shift 
            transmission lever into the manual mode allows the driver to 
            manually shift down or up one gear with each click. Hold the lever 
            to the left for more than a second and it shifts all the way down to 
            the lowest appropriate gear for the speed you're travelling. Hold it 
            to the right for more than a second and it shifts back up to 
            'Drive'. Stop and it automatically shifts down to first. Accelerate 
            away and it shifts back up to the highest gear selected, a good 
            strategy for using third gear when driving around town. <br>
            <br>
            However, the supercharged engine in the C230 is the roughest in the 
            C-Class line. The V6 engines are much smoother. The supercharger 
            makes pleasant mechanical sounds, and it looks like mechanical 
            sculpture under the hood. Yet, one person's sounds are another's 
            noise, and the C230's four-cylinder vibrates more than the V6 both 
            at idle and at high revs. Plus, the four-cylinder engine isn't the 
            best match for an automatic transmission. The automatic zaps much of 
            its zest, and tends to leave only the engine's courser qualities. In 
            other words, the C230 works best with the six-speed manual.<br>
            <br>
            Blasting along at 120 kmph, all C-Class models are reasonably quiet. 
            In the C230 Kompressor that OER test drove, very little wind noise 
            penetrates the cabin. Mercedes-Benz puts as much effort into its 
            braking systems as some makers put into their whole cars, and it 
            shows immediately in the driving. All C-Class models get big brakes 
            (11.8-inch discs up front and 11.4-inch discs in the rear). <br>
            <br>
            Electronic Brake-force Distribution (EBD) automatically distributes 
            brake pressure among the four wheels, adding more to wheels that are 
            gripping best. Brake Assist senses a panic braking situation and 
            helps ensure full braking force even if you make the mistake of 
            relaxing pressure on the brake pedal. In normal driving, the C-Class 
            brakes are progressive in pedal feel; in more serious situations, 
            the anti-lock brakes are enormously powerful and allow the driver to 
            maintain steering control of the car. <br>
            <br>
            <b>Safety</b><br>
            Mercedes prides itself on its safety technology, and every C-Class 
            is equipped with standard safety features at the top of the class, 
            including the Electronic Stability Program (ESP) skid-management 
            system. ESP helps control skids by selectively applying brakes at 
            one or more of the wheels to keep the car on the driver's intended 
            path. Besides the frontal airbags, the C-Class has dual front 
            side-impact airbags and full-cabin curtain-style head protection 
            airbags for front and rear passengers, ABS, Electronic Brake-force 
            Distribution and Brake Assist. <br>
            <br>
            <b>The finishing line </b><br>
            The Mercedes-Benz C-Class offers world-renowned design and 
            engineering for the price of upmarket mid-size sedans. The C230 
            Kompressor is smooth, powerful and comfortable, and delivers a 
            combination of price, safety and utility.  </font></p>
