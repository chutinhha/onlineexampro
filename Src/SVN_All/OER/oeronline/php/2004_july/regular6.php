            competitive. The demand for advertising and marketing services has 
            also increased. This has created a parallel need for a dedicated 
            industry resource where marketers, advertisers and media 
            professionals can update themselves and analyse the latest news and 
            events in the region's business world. Strategiy.com has been 
            conceptualised to respond to this need by delivering a truly 
            comprehensive range of content and services under a single 
            umbrella.&quot; <br>
