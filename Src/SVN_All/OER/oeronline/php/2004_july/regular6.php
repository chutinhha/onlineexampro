            authentic, industry-wide information archive to cover marketing and 
            advertising in the region's burgeoning industry. The interactive 
            portal's content and services include latest news, discussion 
            forums, agency databases, latest marketing campaigns, exhibition and 
            events calendar, interviews and features on Middle East business 
            leaders, appointments and recruitment services, accessible at no 
            charge. <br>
