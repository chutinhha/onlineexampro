            the first five months of this year, the world's oil supply fears 
            were largely focused on OPEC. The oil cartel was steadfastly 
            refusing to pump more crude, despite prices rising towards $40 per 
            barrel (/b). Indeed, as recently as April, OPEC cut crude supply to 
            support prices. Everyone from the US government to British lorry 
            drivers was furious that fuel prices were shooting skywards, and 
            they pointed the finger of blame firmly at OPEC for 'greedily' 
            manipulating prices higher and higher. <br>
            <br>
            Today, it's a slightly different story. OPEC has heeded calls to 
            pump more oil in a bid to bring prices back down to more normal 
            levels (the 10-year historic average for a barrel of crude is around 
            $20). Research by tanker tracker Petrologistics confirms that Saudi 
            Arabia was honoring its pledge to pump 9.1 million barrels per day 
            (b/d) in June, and all the signs pointed to a continuation 
            throughout the summer. But rival supply gremlins have emerged to 
            take OPEC's place. <br>
            <br>
            In Norway, it's striking oil workers. Norway is the world's third 
            largest oil exporter, pumping about 3 million b/d. In late June, 
            some 10 per cent of that output was lost as unions downed tools on a 
            number of Norway's offshore oilrigs. They were complaining about 
            pension rights and working conditions on offshore installations (the 
            thought of oil workers striking in Saudi Arabia, the UAE or Kuwaiti 
            is virtually nil today, although as these economies mature and 
            reform, it may become an issue in the future). <br>
            <br>
            In Iraq, it's terrorists. Iraq lost 10 days of oil exports in June 
            after insurgents bombed pipelines close to key oil terminals in 
            Basra and Khor Al Amaya. By late June, much of the damage had been 
            repaired, but Iraq was still only exporting around 1 million b/d Ð 
            well below its production capacity. <br>
            <br>
            Looking forward, most analysts believe these twin disruptive 
            influences will make supply shortages worse before they get better. 
            In Norway, militant unionists are taking an increasingly hard line 
            in negotiations. It took just 200 workers to halt 10 per cent of the 
            country's output in late June. If more of their colleagues join the 
            strike, as seems likely, the impact on oil exports could be 
            dramatic. ConocoPhillips operates Norwegian fields producing 450,000 
            b/d. ''We are prepared to shut down at very short notice,'' said 
            ConocoPhillips spokesman Ingvar Solberg. <br>
            <br>
            In Iraq, the situation is even tenser. A report by US consultancy 
            PFC Energy warned that June's sabotage attacks on Iraq's oil 
            infrastructure could mark the beginning of a campaign revolving 
            around the handover of power on June 20. ''The attacks will not be 
            the last. As the American role in Iraq becomes less overt and the 
            new transitional Iraqi government seeks to establish its own 
            legitimacy, insurgents have every incentive to embarrass the new 
            leaders, cut off their economic lifeblood and maintain chaos in 
            order to show the government cannot control the country.''<br>
            <br>
            The deteriorating security situation in Saudi Arabia has heightened 
            supply disruption fears. So far, terrorists have not directly 
            targeted the kingdom's oil industry although they have targeted 
            foreign workers in Saudi Arabia's oil industry. Security experts 
            believe Saudi Arabia's hydrocarbon infrastructure is extremely well 
            guarded, but a direct hit could have profound consequences for world 
            supply. <br>
            <br>
            What does this mean for prices? Daniel Hanna, economist with 
            Standard Chartered bank in Dubai, believes prices are likely to 
            remain at historic highs for some time. ''Oil prices have soared 
            with heightened security concerns in the Middle East, most recently 
            in response to a further terrorist attack in Saudi Arabia. With the 
            majority of spare capacity located in Saudi Arabia, the risk is that 
            supplies could be disrupted at a time when demand is booming and 
            OPEC is perceived to have insufficient capacity to fill the gap.''
            <br>
            <br>
            OPEC's decision to raise quotas to 25.5 million b/d at the June 3 
            meeting will provide some relief, but is unlikely to bring prices 
            substantially lower. OPEC is already producing in excess of the new 
            quotas. It has sufficient capacity to meet current demand forecasts, 
            but Hanna argues that there is little room for disruption, and that 
            oil demand forecasts for 2004 may yet prove too low. <br>
            <br>
            ''We see no early resolution to geopolitical concerns and expect the 
            OPEC basket price to average $35/b in 2004. The risks are still 
            firmly on the upside. We anticipate a slowdown in the US in 2005 and 
            moderately slower growth in China. This should ease demand growth 
            and ultimately bring prices lower. However, they are unlikely to 
            fall much below $30/b until geopolitical concerns abate.''
