<p align="justify"><font face="Verdana" size="4" color="#FF0000">Banking &amp; 
Finance<br>
<br>
</font><b><font face="Verdana" size="2" color="#FF0000">New Retail Strategies</font></b><font face="Verdana" size="1"><br>
<br>
<b>Banks and other financial intermediaries can no longer afford to ignore the 
new customer 'revolution', maintains Ali Hassan Moosa </b><br>
<br>
<img border="0" src="bf.jpg" align="left" hspace="6" width="125" height="147"></font><font face="Times New Roman" size="4">B</font><font face="Verdana" size="1">anking 
institutions across the globe are rethinking their traditional lines of business 
to excel in providing customers with service convenience and product clarity. 
Modern retail strategies solely revolve around customers, their needs and 
expectations. Banks and other financial intermediaries can no longer afford to 
ignore the new customer 'revolution'.<br>
<br>
No one, I believe, better describes retail value proposition to customers than 
Sir John Bond of HSBC: &quot;They want integrity - this is their money, these are 
their hard-earned savings, they want somebody that looks after it with total 
integrity. They want fair dealings; they don't want charges introduced without 
proper warning or explanation. They want fair dealing in all aspects of their 
financial affairs, and they want service&quot;. <br>
<br>
The key to retail market penetration is by going back to basics. The customer 
needs/product map describes customer needs based on behaviour/lifestyle and life 
stages/events. Needs may be defined as financial advice, borrowing, protecting 
assets, investing and making payments. Products and services that can be offered 
to meet such needs may include advisory services, personal credit, mortgages and 
brokerage, all within the framework of reliance, dependability and trust. <br>
<br>
<img border="0" src="bf1.jpg" align="right" hspace="6" width="150" height="205">To 
succeed in modern times, financial institutions must adopt a customer-centric 
approach. A customer focused attitude has many dimensions, such as: <br>
<br>
<b>(1) Reach:</b> Financial institutions cannot afford to have branches based on 
their convenience. Banks and other financial intermediaries need to reach out 
and be present where customers are. Two such success stories include: <br>
<br>
i) The Commercial Bank of Qatar started placing small outlets (branches) in 
commercial centres, most notably Doha City Centre. The 
response has been overwhelming as masses of people walk by the outlet every 
single day.<br>
<br>
ii) 'mebank', the retail arm of Emirates Bank International, adopted an 
innovative method of retail representation. 'mebank' concept revolves around a 
franchise enterprise. Its outlets are fully automated with minimum human 
intervention, presented to the public as non-traditional branch banking. <br>
<br>
<b>(2) Innovative products and services:</b> The new generation customer demands 
new types of unconventional products and services offered by modern banks. An 
example of such is Virgin's 'One Account'. A bank account that links all of 
customer's debt instruments such as credit card, loan or a mortgage to short- 
and long-term assets in order to provide the customer with the best packaged 
deal. Benefits of this product include, savings interest is not paid directly to 
the customer but offset against a debt in order to avoid tax, depending on 
customer decision; excess cash can immediately and automatically be used towards 
reducing customer liability, customer controls loan repayments based on life 
events, and actions can be set to take place on daily basis so to avoid customer 
interaction. Now, what chances of customer retention do typical banks have in 
front of a new breed of banks with a strong corporate brand identity offering 
dynamic customer-focused products?<br>
<br>
<b>(3) Comfort and convenience:</b> A contemporary approach to customer comfort 
is to provide new means of integrating branches along with daily customer 
routines. One interesting and successful method in the west was developed by the 
banking giant ING that has more than 110,000 employees across 65 countries. The 
company started a banking retail subsidiary, ING Direct, whose concept is based 
on funky cafés. The formula revolves around a branch that sells simple 
value-for-money products offered in modern cafés. The 
cafés are franchised and as such are run by local entrepreneurs.<br>
<br>
<b>(4) Branding:</b> An essential aspect of a retail strategy, banks need to 
develop a brand strategy that is consistent across all aspects of the 
organisation. The corporate identity must reflect the principles and values of 
the bank. One suggested approach is to have a sharper and a contemporary brand 
image while retaining heritage and culture. <br>
<br>
<b>(5) Human resources:</b> The most important element of a retail strategy, 
bank staff sociology needs to be aligned along with the corporate's 
customer-centric approach. Staff compensation system should purely be 
performance based in order to achieve the maximum output of human interaction.
<br>
<br>
Retail branches need to be planned in such a way as to reflect customer 
behaviour. A zoned approach in a branch may ensure an organised method to 
functional space while retaining flexibility for future modifications with 
minimum business interruption. The design of the branch should be based on 
customer journey and to create an environment that is customer friendly. The 
furniture of the branch needs to be modular, i.e. flexible, to provide comfort, 
convenience and privacy for the customer and must also enforce corporate brand 
identity. <br>
<br>
The latest generation Automated Teller Machines (ATMs) do provide a wide range 
of services. It is believed that such ATMs are capable of processing around 90 
per cent of teller transactions. Utilising such functionality, in theory, could 
free tellers to do more product selling rather than processing customer 
transactions. The physical location of such machines needs to be carefully 
thought. One approach is to place the machines at the entrance of the branch in 
order to eliminate unnecessary traffic inside the branch. The second approach is 
to place some machines next to teller inside the branch. The idea is that once 
customers are fed-up waiting in a teller queue, they may shift to an ATM that is 
located next to them. <br>
<br>
Of course, there are other delivery channels that are a fundamental part of any 
retail strategy such as call centres and Internet banking. Such delivery channel 
usage is ever increasing worldwide as the new generation demands more aggressive 
products available at their convenience and not the bank's. <br>
<br>
Despite high-tech delivery channels, one must not forget the value-addition that 
human interaction can provide. In any event, frontline staff of a bank must be 
capable of handling any customer-related query. In a branch, customers should 
not be pointed to or directed towards different queues and counters to complete 
their transactions. Each and every staff member must be capable of dealing with 
all customer needs. <br>
<br>
In the global marketplace, traditional retail financial intermediaries risk 
becoming non-competitive and, thus, losing market share to contemporary 
retailers unless they re-create a customer-centric value proposition. <br>
<br>
To summarise, branches ought to be treated more like retail shops in terms of 
ambience and physical location rather than traditional and sometimes perhaps 
intimidating places. Staff members need to be sales and relationship driven, 
customer's journey inside the outlet should leave the customer with a 'feel 
good' effect and, of course, the availability of cold and hot drinks inside the 
outlet! </font></p>

