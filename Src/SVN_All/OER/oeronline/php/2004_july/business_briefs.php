                    Further, the beginning value of the MSM 30 index has been 
                    changed to 1,000 from 100, i.e. from three digits to four 
                    digits. The new index represents even the daily marginal 
                    changes in the index, in terms of points, more vivid and 
                    visible compared with the previous index with 100 as the 
                    beginning value. Although percentage change in the index 
                    will remain same, change in points stands more visible. <br>
                    <br>
                    &quot;We are also looking at implementing a new trading system 
                    for the stock exchange by the second half of next year. A 
                    decision on the model and the services provider, who will 
                    supply the new system, will be taken after we complete our 
                    evaluation process,&quot; the director-general said. Al Marhoon 
