                    <p align="justify">
                    <font face="Verdana" size="1"><b>A noble laureate</b><br>
                    Askalan is a titan in his own way. Born and brought up in 
                    Nablus, Jordan, he finished his studies from Al-Najah 
                    National College. A self-made man, he has carved his way up 
                    by sheer hard work. He began his career in 1957 as a clerk 
                    in the Arab Bank in Yemen. Later on, in 1970, he moved to 
                    the UAE to join the bank's branch there. <br>
                    <br>
                    In 1973, when for the first time Arab Bank Oman set up its 
                    branch in the Sultanate, Askalan was entrusted with the 
                    administration of the branches in the country. This was a 
                    time when the bank was making losses. However, with 
                    Askalan's skills and determined efforts, the bank posted 
                    profits within a period of two years. &quot;I reversed the trend 
                    and made the branches profitable for the next 10 years,'' he 
                    beams. <br>
                    <br>
                    From 1984 till date, the equity of OAB has grown 500 per 
                    cent, and the bank has expanded throughout the Sultanate. 
                    From 5 outlets in 1984, there are 45 branches of the bank in 
                    the country today. Askalan successfully steered the 
                    loss-making bank to become one of the leading banks in Oman. 
                    In fact, Askalan proudly recollects that &quot;Arab Bank's Oman 
                    branch opened Oman government's first account to be 
                    independently operated by the Ministry of Finance.'' <br>
                    <br>
                    In 1984, Askalan convinced the management of Arab Bank to 
                    take in an Omani company as partner and offered 50 per cent 
                    of the bank's shares to Oman International Development &amp; 
                    Investment Co. (Ominvest). &quot;Thus, the Omani public got the 
                    opportunity to share up to 50 per cent of the bank's profits 
                    since then,'' he claims. In addition, shareholders of OAB 
                    have received annual dividends of not less than 20 per cent 
                    on an average since the last 19 years. &quot;Ever since, the bank 
                    has grown from strength to strength,'' he maintains. And 
                    there was no looking back for him either. <br>
                    <br>
                    <b>Management mores </b><br>
                    All CEOs have a different style of working. As for Askalan, 
                    he believes in taking the bull by its horns. &quot;As the captain 
                    of my team, I believe in employing a hands-on approach 
                    whenever required,'' he informs. However, he feels it is 
                    important to delegate tasks to his senior officers for the 
                    efficient functioning of the company. &quot;I believe in 
                    delegating tasks to my colleagues as the senior officers 
                    will not be able to develop themselves unless you delegate 
                    tasks to them,'' he says. <br>
                    <br>
                    While Askalan considers his bank employees as the main 
                    pillars of strength of his organisation, for him, his 
                    customers are of utmost importance. &quot;In my philosophy, I 
                    look at customers as our business partners and it goes 
                    without saying, being a partner, we should provide the best 
                    possible service to them at all times,'' he proclaims. <br>
                    <br>
                    <b>Banking challenges </b><br>
                    According to Askalan, the main challenge the banking 
                    industry will continue to face is maintaining strong asset 
                    quality. Here, he has a word of advice for his peers with 
                    ambitions that transcend Oman's borders and go beyond the 
                    country's boundary. &quot;Banks in Oman should not consider 
                    profit as their main motive and should not venture into 
                    unknown territories without a proper feel of that market's 
                    environment,'' he cautions.<br>
                    <br>
                    &quot;Banks wanting cross-border exposures should be physically 
                    present in those locations for a reasonable amount of time. 
                    It is always prudent to spend some time in understanding the 
                    foreign market and then gradually sourcing funds from the 
                    same location and deploying it there,&quot; he advises. Another 
                    challenge, Askalan believes, is competition from 
                    international banks as a result of the WTO (World Trade 
                    Organisation) regulations. He feels the time is apt for 
                    local banks to improve their services if they want to retain 
                    their customer base. Those banks which do not adapt will be 
                    out of business, he foresees.<br>
                    <br>
