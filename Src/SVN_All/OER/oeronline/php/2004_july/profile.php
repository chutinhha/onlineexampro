                    a very distinguished circle of friends. Despite his tight 
                    schedule, he makes a point to take time out for his friends 
                    at least once a week, if not more. He loves spending time 
                    with his dear ones. Besides, he is fond of reading books, 
                    listening to Arabic music and most of all, playing tennis 
                    throughout the week to keep fit. <br>
