<br>
<b>Do high oil prices really damage the world economy? Richard Dean reports</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im26.jpg" align="left" hspace="5" width="150" height="190">US President George W. Bush 
never tires of complaining about the damage that high oil prices cause to the 
global economy. Privately, he is in constant talks with Saudi Arabia, pressuring 
the world's biggest oil exporter to raise production in a bid to lower prices. 
And with crude hovering close to $50 per barrel throughout much of 2004--double 
its historic average--his comments have never been more forceful. <br>
<br>
But are his complaints really justified? Do high oil prices really stifle global 
economic growth? Or is the president simply worried that high gasoline prices at 
US pumps in an election year will hurt his chances of winning a second term in 
the White House? <br>
<br>
First, the evidence supporting the argument that high oil prices hurt the global 
economy. Most economists agree that oil prices do indeed have an impact on the 
global economy. They may disagree about the extent--and the nature--of the 
impact, but there is broad consensus that high oil prices do leave their mark.
<br>
<br>
<b>Stunted growth </b><br>
How does this happen? In a number of ways. First, high crude prices drive up 
domestic fuel prices and, by extension, depress consumer spending and business 
investment. If consumers are spending more money filling up their cars and 
paying their heating bills, they have less money left over for everything from 
washing machines to haircuts.<br>
<br>
A recent study by the Maguire Energy Institute in the US forecast that US 
gasoline (petrol) prices will average up to $2 a gallon in 2004, up from a 2003 
average price of $1.57 and a 2002 average of $1.40. For an average American 
family that drives two cars approximately 15,000 miles a year, that amounts to 
an extra $650 (ro250) in gasoline costs this year. That means $650 less to spend 
on everything else. Add on an average, $100 (RO38.5) or so more on heating and 
air-conditioning bills, and the figure becomes significant for the majority 
families with limited disposable income. &quot;The reality is the vast majority of 
Americans are middle or lower income, and they consume most of the stuff - and 
they're really being creamed by higher gas prices,&quot; says Susan Fulton, a 
principal of US investment management firm WealthTrust. <br>
<br>
<b>Liquidity crunch </b><br>
Similarly, if companies are paying high fuel bills, they have less money left 
over for investment. Indeed, businesses are hit doubly hard, because many of the 
raw materials they use are oil-based. For example, plastic is made from 
petrochemicals, the price of which is directly linked to crude prices. And 
remember that these higher costs come at a time when consumers are spending less 
at cash tills because they've got less money left over after paying their fuel 
bills. Companies are hit on the revenue and expenditure sides. <br>
<br>
A recent study by Royal Bank of Scotland (RBS) in the UK analysed this impact on 
the UK economy, the world's fourth largest. It found that some sectors will be 
hit harder than others. &quot;For some sectors, of course, the negative effects of 
high oil prices would be more concentrated. In the short run, our analysis 
suggests that, as household spending slows in response to the drag on incomes 
from higher energy prices, the hardest-hit sectors would be service and 
consumer-facing industries - particularly where spending is discretionary,&quot; RBS 
highlights electronics, automotive and travel services as particularly 
vulnerable. <br>
<br>
&quot;In the longer term, sectors most adversely affected would be those that are 
most reliant on oil as an input to their production processes.&quot; Utilities, 
transport services and agriculture are among the most exposed to this effect.
<br>
<br>
<b>Inflationary pressures </b><br>
Inflation is also hit by high oil prices. Directly, energy costs make up a 
significant portion of any basket of consumer prices, sending inflation higher. 
Indirectly, companies pass on higher energy and raw materials costs through 
higher prices for finished products ranging from cars to carrier bags. In the 
1970s, the sharp spike in oil prices following the 1973 Arab oil embargo was one 
of the main causes of rampant inflation that saw the global economy stagnate for 
the best part of a decade. <br>
<br>
A recent study by the Energy Information Administration (IEA) and the 
International Monetary Fund quantified this impact. It found that a sustained 
$10 per barrel increase in oil prices from $25 to $35 would result in the OECD 
(the world's advanced economies) as a whole losing 0.4 per cent of GDP in the 
first and second years of higher prices. Inflation would rise by half a 
percentage point and unemployment would also increase.<br>
<br>
<b>Policymakers' dilemma </b><br>
This twin impact--weaker growth and higher inflation--presents policymakers with 
a headache. To stimulate growth, they want to cut interest rates. But to head 
off inflation, they want to raise interest rates. This leads to a conflict for 
the likes of Alan Greenspan, head of the US Federal Reserve and the man charged 
with setting interest rates in the US.<br>
<br>
As OER went to press in late September, Greenspan was poised to reveal the new 
US interest rate. Most economists felt he would raise rates from 1.5 to 1.75 per 
cent - effectively, they feel the main worry is inflation rather than slow 
growth. Either way, Greenspan admits that high oil prices are one of the main 
threats to the US economy at the moment. &quot;If it weren't for the oil price spike, 
I would be very optimistic about where the economy is going,&quot; he said on 
September 8. <br>
<br>
<b>High and dry</b><br>
What about the evidence that supports the counter-argument - that high oil 
prices do not harm the global economy? Certainly, high oil prices are not nearly 
as harmful as they were in the 1970s. &quot;The major industrialised economies are 
much less dependent on oil than they were in the early 1980s, reducing the 
impact of oil price shocks overall compared with the past,&quot; says the report by 
RBS. &quot;Net oil imports now account for only 1 per cent of OECD GDP, compared with 
around 3 per cent in the 1970s. This reflects both improved energy efficiency 
and a shift away from energy-intensive industrial production towards 
service-sector activities.&quot; <br>
<br>
However, this does not mean Bush's fears for the global economy are unfounded. 
Nor are they selfish domestic concerns thinly disguised as global concerns. Yes, 
the US is the world's biggest energy consumer. But a series of studies, 
including those by the IEA and RBS, show that the US is among the least 
vulnerable to high oil prices. <br>
<br>
&quot;Euro-zone countries, which are highly dependent on oil imports, would suffer 
most in the short term, their GDP dropping by 0.5 per cent and inflation rising 
by 0.5 per cent in 2004,&quot; says the IEA. &quot;The US would suffer the least, with GDP 
falling by 0.3 per cent, largely because indigenous production meets a bigger 
share of its oil needs. Japan's GDP would fall 0.4 per cent, with its relatively 
low oil intensity compensating to some extent for its almost total dependence on 
imported oil.&quot; <br>
<br>
<b>Petro-politics</b><br>
Research by the IEA must be treated with some caution. It is funded by the OECD, 
the club of the world's major industrialised nations, including the US. As such, 
some questions may be raised about bias in its findings (although most within 
the industry do regard it as a credible organisation with a reasonable degree of 
independence). <br>
<br>
RBS, by contrast, is a privately owned bank and, therefore, has no political 
allegiance. Significantly, RBS economists came to similar conclusions as those 
at the IEA - that the countries suffering most from high oil prices are not 
heavyweights such as the US and UK, but developing countries. <br>
<br>
&quot;Oil-importing developing countries tend to be worst affected as they are more 
dependent on oil-intensive manufacturing activities, which partly explains the 
large negative impact on China's GDP growth, and are less energy efficient,&quot; 
says RBS. &quot;Furthermore, their access to global capital markets (to smooth oil 
consumption by borrowing) is more restricted.&quot; <br>
<br>
Clearly, George W. Bush does have his own personal agenda when it comes to oil 
price policy. Which politician does not? But on this occasion, it seems his 
complaints about the painful impact of high oil prices on the global economy are 
based on hard facts, not electoral necessity. The author is director of Middle 
East Analysis, a Dubai-based economic consultancy.</font></p>

