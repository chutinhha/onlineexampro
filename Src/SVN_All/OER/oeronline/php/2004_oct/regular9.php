            </b>I graduated in Bachelor of Science in Aviation Technology from 
            the US, along with a pilot's licence. I was also an Associate in 
            Science at the Embry Riddle Aeronautical University in the US. In 
            1984, I joined Oman Air as an aircraft engineer and stayed in that 
            position for two years. After that, I joined the private sector in 
            the trading businesses. Actually, my father needed me to look after 
            those businesses and I had to leave the aviation field reluctantly.
            <br>
            <br>
            From 1986 till 1994, I continued in the private sector, and then I 
            joined Gulf Air after we decided to wind down the businesses due to 
            an oil price slump and the resultant uncertainty. In Gulf Air, I 
            started off as assistant area manager for Australia and New Zealand. 
            This was a complete switch from my earlier innings in the aviation 
            field in the engineering side. Continuing in the commercial section, 
            I was promoted as area manager - Singapore after 11 months. Thereon, 
            I was promoted to the positions of area manager - Kuwait and then 
            area manager - Oman. Finally, I was promoted back to the head office 
            in Bahrain as regional sales manager for Asia Pacific. <br>
            <br>
            I quit Gulf Air in 2001 and joined back Oman Air in the position of 
            senior manager - sales. My recent promotion, in last April, has been 
            as divisional manager - commercial. This is a vice-president level 
            position, and I am in-charge of all the commercial activities of the 
            airline.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Please enlighten 
            our readers about the management structure of Oman Air.</b> <br>
            Oman Air is headed by the chief executive officer. Underneath him, 
            there are six divisional managers. I am in-charge of the Commercial 
            division while my colleagues look after the other divisions - 
            Operations, Engineering, Planning, Human Resources &amp; Administration, 
            and Service Delivery divisions.<br>
            <br>
            <b>What does your role in the company entail?<br>
            </b>I am in charge of the commercial activities of the airline - 
            this includes sale of airline ticket, holiday packages, the 
            marketing aspects of the company, loyalty programmes, tour packages, 
            alliances with other airlines, advertising etc.<br>
            <br>
            <b>The airlines business the world over has been on a
downswing in the past few years. What has been
the impact on Oman Air?</b> <br>
The industry is becoming increasingly difficult to operate
profitably in. Of course, our market is very dynamic and changes happen virtually on a daily basis. To be successful,
you have to not only keep up with these changes, but also be
innovative in coming up with new ideas, new products and
new promotional schemes. We have been doing exactly that at
Oman Air, with the help of an excellent team that I have.
<br>
            <br>
            <b>What other challenges are typical to Oman Air?</b> <br>
There are other airlines in the region that are subsidised by
their respective governments whereas Oman Air is commercially
run. While those airlines are not really selling their product,
we are sticking to our commercial objectives. I am proud to
announce that Oman Air is the only company in Oman to have
been granted an EXIM Bank guarantee from the United States,
which is a major achievement for a private company like ours.
<br>
            <br>
            How have the spiralling oil prices impacted Oman
Air? How do you counter this impact?<br>
The higher the oil price, the better it is for the country, but it is
murderous for the airline industry! For us in Oman Air, the
hike in oil prices really impacted our bottomline because it
was totally unforeseen. Of course, we take precautions and do
fuel-hedging, but nobody was prepared for such a steep rise in
the prices. For us, an increase of 1 baisa in the fuel prices translate
into a RO72,000 hit at the end of the year. Each dollar increase in the price of 
            the barrel costs the aviation industry $1 billion per annum. <br>
            <br>
            We have tried to counter some of the impact by introducing fuel 
            surcharges into the fare, but that covers just one-third of the 
            total cost. To stay afloat, one has to ride the wave with everybody. 
            One has to try to streamline the operations and become a leaner 
            operator. That goes for every facet of the company - for example, 
            the pilot has to look at ways for saving fuel by, say, flying at 
            higher altitudes, or tinkering fuels from a slightly cheaper 
            supplier in the countries one lands etc. Even symbolic gestures like 
            switching off electricity when not in use help. We have regular 
            meetings for rationalising our costs and conduct courses for 
            management and staff to understand the rationale and ways for 
            reducing costs.<br>
            <br>
            <b>Does the entry of low-cost airlines into the market impact Oman 
            Air?</b> <br>
            To a certain extent, it would impact anyone in business, but the 
            model of a low-cost carrier is not being followed by a lot of such 
            players entering the market today. Low-cost airlines depend on 
            multiple frequencies to a destination and the use of secondary 
            airports to reduce their costs, which may not be the customer's 
            destination of choice.<br>
            <br>
            <b>Does Oman Air have any plans of launching a low-cost airline?</b><br>
            No, not at all. <br>
            <br>
            <b>How long have you been in Oman Air, and how
has the company evolved in these years?<br>
            </b>Back in 2000, we couldn't really claim to have an 'airline'. We
had limited network, and virtually nobody knew about Oman
Air except people at the limited destinations we used to fly to.
We can say we started looking more like an airline in 2002,
when we opened eight stations in a year. We began concentrating
on promotions and advertising, and on holiday packages to
bring people into Oman. This is part of our commitment
towards Oman. During the same time, we took the decision to
go in for just two types of state-of-the-art aircraft-Boeing and ATRs-as against 
            the various kinds of aircraft we used to fly earlier. This helped us 
            in cost rationalisation, as each type of aircraft requires intensive 
            training and spare parts. We are now going into the high-tech era, 
            and are looking at a number of IT solutions that will help us be 
            ahead of the competition.<br>
            <br>
            <b>What are the growth/expansion plans of Oman Air?</b> <br>
            We are in talks with the Indian government for flying to two new 
            destinations, viz. New Delhi and Hyderabad. We have also asked for 
            an increase in capacity to our existing destinations in India. In 
            case this doesn't materialise, we do have a back-up plan, and we are 
            looking at various other destinations which I cannot reveal at this 
            moment. We're aiming for a strong Middle East and regional presence 
            for us, better connectivity for the customer, and point-to-point 
            non-stop flight services keeping in mind the customers' 
            requirements.<br>
            <br>
            <b>How do you plan to improve your customer service?<br>
            </b>Internally, there are always on-going training and courses for 
            the staff, following our motto - fly with friends. We always 
            emphasise the importance of the customer, and are investing heavily 
            in customer relations management (CRM), employing managers 
            specialising in CRM to train our staff in the latest and newest 
            techniques of the trade. Now, with the use of IT, we hope to be able 
            to better serve our customers. We will soon launch e-booking and 
            e-ticketing, and we plan to become more IT-savvy and 
            customer-focused. <br>
            <br>
            <b>Briefly describe you management style. </b><br>
            I delegate tasks to the staff with proper authority. I do like the 
            hands-on management approach, but I would only employ it when some 
            of my staff members do not meet my expectations. Time is money for 
            me, and I cannot afford to relax and delay things. I encourage my 
            staff to take decisions and not worry about making mistakes, because 
            that is what experience is all about - learning from your mistakes. 
            I have an open door policy with my staff - anybody, be it a senior 
            or junior member of my team, can just walk in and see me any time of 
            the day. <br>
            <br>
            We also hold regular staff meetings with the section heads within 
            our division, where I get updates on the developments since the last 
            meeting and take decisions about the future direction. The section 
            heads then filter the decisions down to their team members so that 
            everyone down the chain is aware of what is going on. Similarly, I 
            brief the management during our executive management meetings about 
            the proceedings on a regular basis, and communicate any decisions 
            taken in such meetings to my section heads to accordingly brief 
            their team. <br>
            <br>
            I encourage communication horizontally and vertically because 
            communication is one of the most important things in any 
            organisation. If my team members are not aware of the goings-on, 
            they would not be able to come up with innovative ideas. You can get 
            the brilliant ideas from the junior-most staff. Information is 
            power, and knowing vital information about what's going enables 
            everyone to perform their jobs better. Everyone in my division has 
            the big picture and the non-confidential information necessary for 
            them to function at their best performance levels.<br>
            <br>
            <b>Which management gurus do you favour and why? </b><br>
            None. I read a lot of management books, but have no real favourites. 
            From what I have read, the theories and experience these books 
            propound are relevant in another market. I do pick up the best 
            practices that can be adapted to our industry and market, but 
            honestly, following the methods preached by any one guru are not 
            going to work in our business.</font></p>
