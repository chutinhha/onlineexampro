<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">'Ad One 
Too Many?</font></font><font face="Verdana" size="1"><br>
<br>
<b>Matt Harrison discusses the relevance of straplines and suggests ways of 
arriving at a cracker of a strapline - the greatest attention getter in 
advertising today</b><br>
<br>
What is a strapline - a slogan, a claim, an endline, a signature? It's Ikea's 
'Stop being so English', Nissan Micra's 'Ask before you borrow it', Nike's 'Just 
Do It', DeBeers' 'Diamonds are forever' and the Renault Clio's 'Size matters.' 
It's a verbal logo - it sums up what a company stands for and copywriters the 
world over will burn the midnight oil to produce that perfect set of memorable 
words for a company to live by. Once a killer strapline has been born, its 
exposure isn't just limited to media advertising, it's on everything from 
business cards to brochures, match boxes, coffee coasters, mouse pads, T-shirts, 
to the back of buses. <br>
<br>
Many in advertising believe a strapline to be crucial, it's the desire to see 
the brand pithily summed up and encapsulated in words which hit home the 
advertising message and leave consumers with a clear thought about the brand. 
Charles L Whittier, (Creative Advertising) says it's &quot;a statement of such merit 
about a product or service that it is worthy of continuous repetition in 
advertising, is worthwhile for the public to remember, and is phrased in such a 
way that the public is likely to remember it.'' To which I'd add: The purpose of 
the strapline is to leave the key brand message in the mind of the target 
audience. It's the sign-off that accompanies the logo. It says ''If you get 
nothing else from this ad, get this!'' <br>
<br>
The top 10 US advertisers spent $15 billion (RO5.77 billion) in 2003 (up 8.2 per 
cent on 2002) on devising and promoting catchy advertising campaigns. However, 
research suggests that much of this spend is lost in the clutter or simply 
forgettable. Indeed, a recent survey reveals that many of the world's leading 
straplines are being misidentified. Take the automobile industry as an example - 
although 60 per cent of respondents were familiar with BMW's 'The ultimate 
driving machine', only 11 per cent knew it belonged to the German car 
manufacturer. Chrysler's 'Engineered to be great cars' didn't do much better. 
Chevy Trucks' 'Like a rock' was recalled by an impressive 9 out of 10 
respondents, but when they were asked what the product was, less than 40 per 
cent were able to identify it. Nissan did even worse as only 8 per cent knew it 
was the originator of the strapline 'Enjoy the ride.'<br>
<br>
Non-recognition also applies in the fast food business - Burger King's 'It just 
tastes better' faired better than McDonald's 'Did somebody say McDonald's?' line 
in terms of recognition, 79 per cent to 54 per cent. But when asked which 
company belonged to these straplines, only 14 per cent correctly identified 
Burger King's while 34 per cent knew McDonald's. Naturally, the McDonald's brand 
name was concealed in the survey. <br>
<br>
Other firms who shared this 'heard, but not associated' fate were Visa and 
General Electric. Visa's line 'It's everywhere you want to be,' was 'heard' by 
70 per cent of respondents, but only 15 per cent were able to make the 
connection between the strapline and the credit card company. For reference, 
American Express' 'Do more' (19 per cent), Blockbuster's 'Go home happy' (18 per 
cent) and United Airlines' 'Rising' (10 per cent) were the least recognisable of 
all the straplines tested. <br>
<br>
What does this tell us? Quite simply, today's ad straplines are failing to 
register with consumers. Overall, the survey revealed that 16 of the 19 
straplines tested were 'heard' or 'recognised' by at least 50 per cent of the 
survey respondents. However, a remarkably high number of consumers were unable 
to correctly identify which brand or product these straplines were promoting.
<br>
<br>
So what do you do? Forget straplines? I don't think so. Let's face it - a 
classic line can be worth its weight in gold. If nothing else, it's a great 
attention getter. In fact, it can form the base for an entire advertising 
campaign, one that rolls on year after year. So if you're running an Oman-based 
company and folks haven't connected your outfit with your strapline, here are 
some tips on cracking the problem. <br>
<br>
1) Start by looking at the ads on billboards on the highway, at airports and in 
shopping malls. These advertisers have but a couple of seconds to grab your 
attention and sell their product or service before you drive or walk by. 
Usually, the strapline comes with a picture of the product or service. Nike has 
its swoosh and 'Just do it'. Each is recognisable and clearly associated with 
Nike as a brand. If you see or hear 'Think different' or 'Vorsprung durch 
Technik', I think you'll also imagine the Apple logo and the Audi rings. But, 
from the words, you get a much deeper sense of the brand's meaning than you 
would from looking at the logo alone. In short, words reinforce images, images 
reinforce words. Get both right and you should create a powerful brand. <br>
<br>
2) Notice other media forms like magazine and newspaper display ads, business 
cards, radio and TV commercials. Think about the thing that caught your 
attention and makes the message easily remembered. It's usually a concise and 
well-written strapline. <br>
<br>
3) Write down everything you can think of that relates to your business. You may 
even start with a narrative description in paragraph form. Then make a list of 
the top 25 or 30 things that are important and worth mentioning. Cut that list 
down to 8 or 10 of the most important things you want to say. Eliminate 
repetition or things that aren't really that necessary to your product or 
service. Get the list of words or phrases down to 3 or 4 central elements. <br>
<br>
4) Based on your final core selection, make up some phrases that will serve as 
your straplines for consideration. Keep them short and use simple, everyday 
language - as David Ogilvy once commented: ''I don't know the rules of 
grammar... If you're trying to persuade people to do something, or buy 
something, it seems to me you should use their language, the language they use 
every day, the language in which they think.'' To highlight this, consider the 
following straplines, which of these would you remember best?</font></p>

<p align="center"><font face="Verdana" size="1">'Ali's garage, the lowest-prices 
and the best service' <br>
Or<br>
'Quality Care For Your Car!'<br>
<br>
'The Best Accountants in Town!'<br>
Or<br>
'Your Tax Experts At Work!'<br>
<br>
'Taxi service available 24-hours a day' <br>
Or <br>
'When Seconds Count!'
</font></p>

<p align="justify"><font face="Verdana" size="1">But do consumers even notice or 
care whether an advertisement or campaign carries a strapline? In preparation 
for this article, I asked a number of consumers this exact question. In general, 
and as noted earlier, the consensus was that so many straplines fail to 
register. This isn't to say however, that consumers don't enjoy and respond 
positively to straplines and there's no doubt that amongst many people, 
particularly the under 25s, straplines still become incorporated into everyday 
language and take their place in youth culture. <br>
<br>
This in turn has the effect, not only of keeping a particular brand name top of 
mind, but bolsters the consumer relationship with advertising, keeping it as 
salient a part of their lives as are some of their favourite films and TV 
programmes. This can be a clear bonus of having (good) straplines; if they get 
talked about, remembered and used in common language, they help keep the world 
of advertising alive and well and living in consumers' minds. Yet, this does of 
course depend on the extent to which there are good, memorable, catchy 
straplines 'out there' which also have positive associations for the brand. <br>
<br>
In the final analysis, strong straplines are those that can live divorced from 
the creative yet carry with them the creative idea and an image of the brand. 
The benchmark of any good ad campaign has to be the lasting impression it has on 
the consumer. Incorporating the brand name in the logo helps achieve this. 
That's no doubt why marketers that feature their name in the straplines--like 
McDonald's did--will always do better than others in connecting with the 
consumer. The more a strapline--as well as advertising--is inextricably linked 
to the brand, the more effective it will be. This isn't brain surgery, is it?<br>
