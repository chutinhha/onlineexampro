            <font face="Verdana" size="4" color="#FF0000">Life beyond Oil!</font><font face="Verdana" size="1"><br>
<br>
            <b>Oman's privatisation programme is at full throttle, having 
            delivered a succession of power projects to international 
            developers, and making good on promises to open up 
            telecommunications and airport and seaport management to private 
            investment. Now the government is turning its attention to 
            disinvesting power generation and water desalination infrastructure, 
            postal services and public transport - measures that are earning it 
            the quiet admiration of many countries in the Gulf and Arab region. 
            An OER report. </b><br>
            <br>
            <img border="0" src="im28.jpg" align="left" hspace="5" width="150" height="145">Like most Royal Decrees, the Privatisation Law was promulgated with 
            little fanfare - a short, terse report front-paged in the national 
            dailies and highlighted in news bulletins on television and radio. 
            But, if anything, Royal Decree No. 77/2004, issued on July 17 this 
            year, had one immediate impact - it silenced doubters, if any, of 
            the government's commitment to privatisation and liberalisation. <br>
            <br>
            <b>Mission statement </b><br>
            Unquestionably a landmark statute, Royal Decree 77/2004 has been 
            widely hailed in capitals within and outside the Gulf region, as a 
            mission statement that robustly affirms the Omani government's 
            determination to press on with its privatisation programme. It 
            firmly sets the government's imprimatur on a process that in reality 
            began in the mid-1990s, when the Gulf's first private-led power 
            project was established in the Sultanate.<br>
            <br>
            Since then, several groundbreaking deals have been signed allowing 
            private investment in areas as diverse as air and seaport operation, 
            telecommunications, higher education, healthcare, tourism resorts, 
            and the key power and water sector. Soon, foreign ownership of real 
            estate - dubbed the holy grail of private reform and a huge catalyst 
            for foreign investment - will be added to the statute books. <br>
            <br>
            Privatisation remains the cornerstone of the government's Vision 
            2020 charter, launched in 1995 as a blueprint for economic 
            development spanning 25 years. The charter envisions a dominant role 
            for the private sector in national development, as well as calls for 
            a vigorous thrust towards a non-oil-based economy - ambitious 
            targets for a nation that derives 80 per cent of its export earnings 
            from oil, and depends on generous government spending to drive 
            domestic growth. <br>
            <br>
            <b>Manah power project </b><br>
            But against all expectations, the privatisation drive got off to a 
            momentous start in 1996 with the launch of the Manah power project - 
            the Gulf region's first Independent Power Project (IPP). Belgium's 
            Tractebel-led United Power Company won a concession to construct a 
            90MW gas fired power station, conceived on the lines of the 
            traditional build-own-operate-transfer (BOOT) model. The developer 
            has since tripled generation capacity to 270MW, bringing the 
            additional capacity into operation in May 2000. <br>
            <br>
            Hailed as a pioneer in power privatisation, the government then 
            chalked out an elaborate list of infrastructure and services to be 
            placed in private hands. What followed however was a three-year-long 
            hiatus as planners mulled the best way forward in establishing a 
            privatisation framework robust enough to attract foreign interest 
            and, more importantly, investment. Then, hit hard by the oil price 
            crash of 1998, and coupled with the sobering realisation that 
            globalisation would soon come knocking, the government injected new 
            momentum in the privatisation drive. It began by initiating sweeping 
            private sector reform, promoting transparency, modernising the legal 
            system and establishing an independent judiciary, among other 
            measures. The upshot was to create an investor-friendly regime that 
            has since brought international heavyweights knocking on the 
            Sultanate's doors. <br>
            <br>
            Privatisation has since become an inexorable force - a few bumps in 
            the road notwithstanding. A flurry of independent power projects (IPPs) 
            has since confirmed the Sultanate as a trendsetter in private sector 
            reform. The first to be awarded was the 285 MW gas-fired power 
            project at Al Kamil in the Sharqiya region in July 2000. Al Kamil 
            Power Company, led by Britain's International Power, has developed a 
            state-of-the-art generation facility at a cost of $130 million (RO50 
            million). <br>
            <br>
            <b>Barka water and power project </b><br>
            Barely four months later, the government named US-based AES 
            Corporation and its local partner Multitech as developers of Barka 
            independent water and power project (IWPP) - then the largest 
            greenfield power scheme in the Sultanate. It involved the 
            construction of 427MW of power generation capacity and 40 million 
            gallons/day desalination scheme at a total cost of around $455 
            million (RO175 million).<br>
            <br>
            Salalah power unit New ground was broken in March 2001 when the 
            government signed a concession agreement paving the way for the 
            establishment of Oman's first fully privatised, vertically 
            integrated power utility in Salalah. Dhofar Power Company, led by 
            American energy giant PSEG Global, was handed the concession to 
            build a 200MW gas powered plant at a cost of $270 million (RO103 
            million). The company also won the mandate to acquire and modernise 
            existing generation, transmission and distribution assets and, 
            crucially, handle billing and collection as well. It is the first 
            such award of its kind in the Middle East where power generation, 
            transmission, distribution and billing is handled by a private 
            utility.<br>
            <br>
            <b>Sohar desalination project </b><br>
            In July, the government invited a consortium led by Suez-Tractebel 
            of Belgium to build the biggest greenfield power and desalination 
            project at Sohar. Sohar Power Company, which groups Suez-Tractebel 
            and several Omani promoters, will invest more than RO200 million in 
            developing a 585MW power generation plant alongside a 33 
            million-gallons/day desalination facility. <br>
            <br>
            What distinguishes these projects from IPPs developed elsewhere in 
            the region is that the Omani model is based on 100 per cent 
            ownership by the private developer. The government has pledged to 
            continue promoting this model, but nevertheless requires the 
            promoter to offload at least 35 per cent of its stake on the Muscat 
            Securities Market through initial public offerings (IPOs). These 
            IPOs must be floated within four years of the incorporation of the 
            company. <br>
            <br>
            First off the mark was Al Kamil Power Company, which floated its IPO 
            in July this year. The share issue was oversubscribed, underlining 
            the strong appeal of foreign companies to local shareholders. AES 
            Barka Power Company, which operates the IPP at Barka, will follow 
            suit with its own IPO in November this year. <br>
            <br>
            But the crowning moment for power sector privatisation in the 
            Sultanate came on July 21, when the Sectoral Law for the Electricity 
            &amp; Water Sector was promulgated by Royal Decree. It confirms a widely 
            published plan for the privatisation of existing state-owned 
            generation, transmission and distribution assets - a blueprint 
            without precedent in the Gulf. <br>
            <br>
            The Sectoral Law follows a lengthy unbundling process that has 
            resulted in the creation of a parent holding company, as well as 
            nine generation, transmission and distribution companies, prior to 
            their corporatisation and privatisation. The latter nine entities 
            will initially operate under the umbrella of a government-owned 
            Electricity Holding Company, which will monitor and supervise their 
            accounts and functions prior to their privatisation. Included among 
            the nine entities are a Power &amp; Water Procurer and a Rural Areas 
            Electricity Company, each tasked with specific responsibilities. <br>
            <br>
            The new market structure for the power and water sector envisions 
            three Power Generation &amp; Water Desalination Companies, which will 
            each acquire the existing power plants at Rusayl, Al Ghubra and Wadi 
            Jizzi. Conceived initially as government-owned joint stock 
            companies, these firms will be later privatised to operate on the 
            lines of IPPs such as those at Manah, Barka, Al Kamil and Salalah.
            <br>
            <br>
            Also envisaged is a Power Transmission &amp; Despatch Company, which is 
            tasked with two basic functions. Firstly, it will manage the 
            government's high voltage power transmission system by transmitting 
            the generated power from the generation companies to the 
            distribution companies. Secondly, it will oversee the dispatch of 
            network loads according to the technical, economic and contractual 
            criteria agreed between the generation and distribution companies.
            <br>
            <br>
            On the distribution and supply side, the northern Oman areas will be 
            divided into three concession areas each managed by a separate 
            distribution company. Each entity will oversee power distribution 
            and supply in its respective concession area, as well as the 
            collection of revenues. The proposed companies will be named: Muscat 
            Electricity Distribution Company, Mazoon Electricity Company and 
            Majan Electricity Company.<br>
            <br>
            Prominent among the nine entities is the Power &amp; Water Procurer (PWP) 
            whose primary brief is to prepare demand forecasts for both power 
            and water. The PWP will also plan the energy supply volume and make 
            the necessary contracting arrangements with the power and water 
            producers. In essence, the PWP will assume the role of the main 
            purchaser of the entire output of power and water. <br>
            <br>
            Ownership of the PWP will remain in government hands until a 
            competitive environment is created and permission obtained for 
            direct contracting between the generation and distribution 
            companies. <br>
            <br>
            Finally, a Rural Areas Electricity Company will be created to 
            oversee generation, distribution and supply in rural areas of the 
            Sultanate, notably islands and remote villages that are not 
            connected to the national grids. The sectoral law prohibits the sale 
            of this entity. <br>
            <br>
            Significantly, an independent authority will be established by the 
            government to regulate the electricity and related desalinated water 
            sector according to the sectoral law. The primary duties of the 
            regulatory authority, which will be financially and administratively 
            independent, are to protect and promote consumer interests, as well 
            as to encourage competition. <br>
            <br>
            Additionally, the body will also be called upon to ensure safe, 
            secure and efficient power supply; facilitate meeting of demand; 
            ensure compliance with regard to environment, Omanisation, and so 
            on; and make sure that licence holders are able to finance 
            activities. A non-discriminatory approach will be adopted in its 
            dealings with the industry.<br>
            <br>
            All nine companies, and the apex Electricity Holding Company, have 
            since been created by the government. The requisite operating 
            licences have been drafted and industry codes formulated as well.
            <br>
            <br>
            The restructuring plan has several significant underlying 
            objectives. Foremost, it will ensure continuity of power and water 
            supply to consumers by allowing for the efficient expansion of the 
            network in line with demand. It would also pave the way for gradual 
            reductions in government subsidies by making the power and water 
            sector more competitive and transparent. <br>
            <br>
            Additional gains will accrue in the form of foreign capital inflows, 
            technology transfer, economical use of gas fuel through use of 
            modern technology, lower network losses, energy and water 
            conservation, and better utilisation of the load factor in summer.<br>
            <br>
            <b>Airports </b><br>
            But it is Oman's successes in areas like air and seaport management 
            that attest to the far-reaching nature of the government's 
            privatisation drive. Stealing a march over its Gulf neighbours, the 
            Sultanate awarded a 25-year concession to a majority privately owned 
            company to manage and upgrade Seeb and Salalah airports in 2001. 
            Oman Airports Management Company (OAMC) will invest around RO60-70 
            million in a new terminal building, among other facilities, at Seeb 
            International Airport. The upgrade, which includes new jetties, 
            departure lounges and passenger gates, will boost the terminal's 
            capacity to around 6.5 million passengers per year, up from the 
            present 2.8 million passengers.<br>
            <br>
            OAMC is backed by British Airports Authority, which together with 
            Suhail Bahwan Group and ABB Equity Ventures, owns 75 per cent of the 
            stake in the company. The government has a 20 per cent stake, while 
            Oman Aviation Services owns the remainder five per cent. The 
            operator is primarily in charge of airport management and ground 
            handling functions, while the government continues to be responsible 
            for air traffic control, flight safety, meteorology, immigration, 
            customs and security. <br>
            <br>
            The downturn in air travel, sparked by the events of September 11, 
            has to some extent put the brakes on OAMC's ambitious plans for a 
            major overhaul of Seeb and Salalah airports. But the operator is 
            determined to forge ahead with a revised plan that reflects present 
            trends in commercial air traffic and passenger movement. <br>
            <br>
            For Salalah Port Services, the operator of the Port of Salalah in 
            the south of the country, finding customers for its state-of-the-art 
            transshipment terminal has hardly been a concern. On the contrary, 
            its main challenge is to address potential capacity constraints as 
            existing and new shipping lines choose Salalah as a hub for their 
            container traffic - in what is a reflection of its phenomenal 
            success since it was established in 1998. <br>
            <br>
            Over the last six years, the port has grown from a greenfield site 
            into a transshipment hub handling volumes in excess of 2 million 
            Twenty Equivalent Units (TEUs) per annum. In less than five years 
            since the start of operations, the port's cumulative throughput has 
            reached over 5 million TEUs. This achievement has made it one of the 
            fastest growing ports in the world and one of the fastest in history 
            to reach this milestone. <br>
            <br>
            The port is currently operating above its berth capacity and 
            development of additional berths at the port, along with the 
            expansion of breakwater infrastructure, holds the key to further 
            growth. Two new berths will be added to the port infrastructure as 
            part of an expansion plan involving a total investment of around 
            $249.2 million (RO96 million). While the government will finance 
            upgrades to the port infrastructure, SPS will invest around $82.8 
            million (RO32 million) in providing the superstructure for the new 
            berths.<br>
            <br>
            SPS, a public Omani company which also acts as the port authority, 
            is 30 per cent owned by A.P. Moller Terminals, which is part of a 
            huge conglomerate with interests in shipping, shipbuilding, 
            aviation, oil and gas, industry and IT services. The government has 
            20 per cent of the equity. <br>
            <br>
            Emboldened by its success in Salalah, the government acted similarly 
            to secure a heavyweight as its partner in the operation and 
            management of the Sohar Industrial Port on the Batinah coast. <br>
            <br>
            In July 2002 the government awarded a 25-year concession to Sohar 
            Industrial Port Company (SIPC) to undertake the development and 
            management of the port and industrial complex aggregating some 2,000 
            hectares. SIPC is a joint venture of the Omani government and 
            Rotterdam Municipal Port Management (RPMP), the port authority of 
            the world's premier maritime hub at Rotterdam in Holland. <br>
            <br>
            Since that landmark deal, RPMP has brought its enormous expertise to 
            bear on the establishment of what promises to be a huge gas-based 
            industrial and petrochemical hub at Sohar. Port Rotterdam is 
            providing its know-how and experience in developing and managing a 
            large multi-user port and industrial complex. Already investments 
            for a refinery, polypropylene plant, polyethylene project, 
            petrochemical unit, methanol scheme and aluminium smelter, totalling 
            in excess of $6 billion have been committed by various private and 
            government promoters. <br>
            <br>
            <b>Telecommunications </b><br>
            In the telecom sector, however, the privatisation effort has panned 
            out in fits and starts. But a government pledge to open up the 
            sector to competition was fulfilled last June when Nawras Telecom 
            was awarded a licence to operate a second mobile service in Oman. 
            The landmark award effectively brings to an end state-owned 
            Omantel's roughly 30-year monopoly as the sole provider of telecom 
            services in the Sultanate. <br>
            <br>
            Nawras Telecom, a consortium grouping Qatar Telecom (Qtel), TDC 
            Mobile International of Denmark, and Omani pension funds, will 
            invest over $1.7 billion (RO654 million) in the creation of new 
            telecom infrastructure in the Sultanate over the next 10 years. The 
            new operator will also create around 1,600 new jobs either working 
            directly for the company or indirectly for supporting industries.
            <br>
            <br>
            The consortium won this licence against a particularly strong 
            international competition of 28 consortia including Vodafone and 
            Orange as well as the elite of the regional operators with MTC and 
            Cable &amp; Wireless/Batelco. <br>
            <br>
            World Trade Organisation (WTO) regulations require member states to 
            liberalise their telecom sectors by 2005. In line with this 
            obligation, the government took steps to open up this sector to 
            foreign competition. As a first step, it promulgated the 
            Telecommunication Regulatory Act in 2002, which enshrines the powers 
            of a new telecom regulatory authority. <br>
            <br>
            Telecom liberalisation will also open up access for public data 
            networks, value-added services, prepaid calling cards for fixed, 
            mobile and Internet services, and leased lines and private data 
            networks. <br>
            <br>
            The launch of the bidding process for the second mobile licence 
            preceded a period of significant structural changes at Omantel. The 
            government had initially intended to offer 40 per cent of Omantel's 
            stake to a strategic investor who would then modernise the 
            infrastructure. <br>
            <br>
            A further nine per cent was envisaged for institutional investors, 
            with the government holding the remaining 51 per cent. But dramatic 
            upheavals in the global telecommunications sector proved 
            inauspicious. Last year, the authorities firmed up a plan to offer 
            30 per cent of Omantel's equity for public subscription in the form 
            of an IPO. In preparation for this partial sell-off, the government 
            announced plans for the creation of a holding company with the 
            assets of Omantel split up among three separate corporate entities 
            each handling the company's core businesses - fixed line services, 
            mobile services, and Internet services.<br>
            <br>
            The first of these entities came into being in March this year. Oman 
            Mobile Telecommunications Company (OMTC) was created in line with a 
            Royal Decree that granted Omantel two licences, the first to provide 
            fixed telecom services for 25 years and the second, basic mobile 
            services for 15 years. It is the first telecom firm licensed to 
            provide mobile telecommunications services in the Sultanate. <br>
            <br>
            <b>Waste management </b><br>
            Oman also hopes to break new ground when it invites a private firm 
            next year to establish a National Hazardous Waste Management project 
            in the Sultanate of Oman.<br>
            <br>
            Envisaged is a single vehicle project company that will provide 
            complete 'cradle to grave' collection, reception and/or recycling, 
            treatment and disposal facilities for all hazardous wastes generated 
            in the country that require secure management and disposal. <br>
            <br>
            The company will be awarded a 25-year concession to develop the 
            scheme on a design-finance-build-own-operate-and-transfer (DFBOOT) 
            basis. The concessionaire will be responsible for financing all 
            project infrastructure and operational expenses, with the project 
            costs recovered through a levy earned from the waste generators. <br>
            <br>
            <b>Postal and bus services, industrial estates </b><br>
            Also touted for eventual privatisation are the postal services, Oman 
            National Transport Company (ONTC) as well as the industrial estates. 
            Over the last 18 months, the government has also offloaded part of 
            its equity stakes in certain listed firms, such as Oman Cement 
            Company, Oman Flour Mills and Al Maha Petroleum Marketing Companies.<br>
            <br>
            <b>Modernisation of legal system </b><br>
            Concurrently, the government has been pressing ahead in modernising 
            its legal system. Foreign ownership in companies has been increased 
            from 49 to 65 per cent, while a 100 per cent foreign stake is also 
            possible in infrastructure projects. <br>
            <br>
            Tax laws have been amended to ease the tax regime applicable to 
            foreign investors.<br>
            <br>
            Other investment sops come in the form of soft loans, easy repayment 
            terms, customs duty waivers on equipment, and on raw materials for 
            up to 10 years, corporate tax holidays for up to 10 years, full 
            repatriation of capital, net profits and royalties, and export 
            credit insurance. Above all, government processes have become highly 
            transparent, offering a level playing field to all those who seek to 
            invest in Sultanate of Oman.</font></p>
