<p align="justify"><font face="Verdana" size="2" color="#C0C0C0">:: Car Review</font></p>

<p align="justify"><font face="Verdana" size="4" color="#FF0000">Classy elegance</font><font face="Verdana" size="1"><br>
<br>
<b>The Lexus ES300 is possibly the smoothest, quietest car in its class. Vicky 
Kapur takes the hot new 2005 300 for a spin around town</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im24.jpg" align="left" hspace="5" width="140" height="146">It's easy to live with the ES. Not only is its 3-litre V6 engine and five-speed 
automatic transmission smooth and responsive, this RO15000 entry-level car comes 
loaded with features that can turn driving into a much-looked-forward to event 
in your otherwise drab workday. A ride in the new ES300 feels exceptionally 
elegant, as if riding a thoroughbred racehorse, except for the fact that this 
stallion of steel will keep you and your four co-passengers comfortably cocooned 
in its safety nest even at a top speed of 220 km per hour. <br>
<br>
Its steering is effortless, and its supple suspension gives it a silky ride - be 
it on the immaculately maintained Muscat motorways or the under-construction 
back roads to the Yeti beach. And its anti-lock brakes are easy to modulate for 
smooth stops in everyday driving. The seats are comfortable, and the interior 
has a rich appearance, trimmed in the leather, wood and faux aluminium. Most 
features are straightforward and easy to operate. There's plenty of room for 
passengers in the back seats, which feature outboard seatbelts with pre-tensioners 
and force limiters. And there's the trunk which is the largest in its class. <br>
<br>
Refinement, quality fit and finish, and attention to detail are evident 
throughout. You see and feel the quality. You can hear it in the solid sound the 
doors, trunk, and hood make when closed. The 2005 Lexus ES300 gets a new 3-spoke 
steering wheel design with audio controls, 6-step seat ventilator dials above 
the AC control panels and front-seat ventilators-blower function, among a host 
of other things. Exterior styling revisions freshen the looks of the 2005 ES300.
<br>
<br>
<b>The outdoors </b><br>
Mild styling revisions distinguish the 2005 ES300 from last year's model. The 
fog lamps, grille, lower intake, headlamps, and tail lamps have been revised, 
giving it a fresh look. The rake of the ES300 combined with its streaked-back 
xenon headlamps make a striking statement. The front end seems to slope down in 
one continuous curve, from the top of the windshield, over the sculpted hood and 
tidy dark grille, down to the smooth air dam under the almost seamless front 
bumper. The headlight cluster suggests silvery eyes pulled back at the corners.
<br>
<br>
Tail lamps trimmed in silver echo the design, stretching around the corners, as 
if reaching to meet the headlamps. The standard nine-spoke 16-inch alloy wheels 
complement the styling. Chromed door handles are easy to grab and do not require 
awkwardly flipping your hand over as they do on some of the other cars in this 
class. <br>
<br>
The ES300 is based on a tried-and-tested platform. The underbody is smooth, 
nearly flat, contributing to exceptional aerodynamic efficiency (with a 
coefficient of drag of just 0.28), and the car is constructed using advanced 
composites and resin-steel sandwiches to further minimise the intrusion of 
noise. <br>
<br>
The High Intensity Discharge (HID) low beam headlamps emit a high quantity of 
light for excellent visibility. An auto-levelling for the HID headlamps keeps 
the direction of the beam constant for stable visibility at night. <br>
<br>
<b>The indoors </b><br>
The Lexus ES300 presents a warm, inviting interior. Rich wood trim goes well 
with the brushed aluminium-like trim. All of the trim materials exude a 
high-quality look and feel. The standard steering wheel and shift lever are 
covered with stitched leather-trim and look very nice, and are also available in 
wood trim. The steering column is manually adjustable for tilt. New steering 
wheel-mounted controls are available that make operating the audio system much 
easier.<br>
<br>
The standard leather-trimmed upholstery is quite nice and the seats are very 
comfortable. There's lots of front-seat legroom and hip room, though tall 
drivers may wish for more headroom. The seats are relatively flat, without large 
bolsters, making getting in and out easier. The driver's and front passenger 
seats come standard with 8-way power adjustments. The shape and construction of 
the front seats seem to have been borrowed from the flagship LS430. <br>
<br>
The driver's seat offers good visibility out front. The self-dimming (electrochromic) 
outside mirrors offer a good view rearward and seem larger than those on some of 
the Mercs. The inside rearview mirror is self-dimming too, and features a 
digital compass. The slope of the rear window doesn't allow a panoramic view, 
though it fills the rearview mirror. On the 2005 models, both outside mirrors 
automatically tilt down if desired when you shift into reverse. An optional 
power rear shade filters out the blazing sun, whether driving or parked, then 
automatically lowers whenever you shift into reverse. <br>
<br>
The clean-looking instrument panel features three round, deeply recessed pods 
with highly legible optitron gauges. The instrument panel comes in a lighter 
silver finish for 2005 with colour-coordinated light grey buttons. A large 
speedometer dominates the layout. A digital trip computer at the bottom of the 
speedometer displays range, average fuel consumption and other functions, 
controlled by a pair of buttons on the left side of the steering wheel. 
Similarly, a digital odometer with two trip odometers is displayed in the right 
pod with big fuel and temperature gauges. On the left is a big tachometer. <br>
<br>
The Bluetooth-enabled advanced car kit allows you to use your cellular phone 
while driving your car with the best of comfort and the best security possible. 
The functionalities of the system include caller name display, voice 
recognition, volume control, muting of the the in-car radio, dual call and 
pick-up and hang-up via voice command. The sound quality from the audio system 
is fantastic, especially when a CD is playing. Combined with the sound-absorbing 
measures in the ES300 cabin, it provides the audiophile with crisp, clear highs 
and lows and enveloping mid range. <br>
<br>
<b>Performance </b><br>
The Lexus ES300 delivers a smooth, refined driving experience with a smooth 
engine and transmission, a smooth-riding suspension, smooth brakes, and 
extensive measures designed to reduce noise, vibration and harshness. The noise 
damping structure is designed to absorb noise, and silence is the first thing 
one hears once behind the wheel.<br>
<br>
The engine is silky smooth and whisper quiet. The 3-litre V6 engine produces 232 
horsepower and 30.5 kg-m of torque and delivers responsive performance in 
everyday driving situations. The ES300 can accelerate from 0 to 100 km per hour 
in 8.7 seconds, according to Lexus. <br>
<br>
The transmission shifts very smoothly. Upshifts in normal, everyday driving are 
almost invisible. Yet, it delivers responsive shifts when driving aggressively. 
The gated shifter allows manual shifting and an indicator displays the selected 
gear on the instrument panel, but shifting manually is a clunky proposition due 
to the gates and we preferred leaving it in Drive. The 5-speed automatic super 
ECT (Electronically Controlled Transmission) incorporates a highly efficient 
'Super Flow' torque converter and uphill/downhill downshift control. It delivers 
a smooth acceleration with superb fuel efficiency. <br>
<br>
There is a 'Sport' mode for those bursts of inspirations required to negotiate a 
tricky roundabout or the omnipresent inclines on Muscat roads. True to its 
class, the ES300's suspension is tuned for a luxurious and smooth drive. It 
filters out vibration and contributes to the ES300's exceptional quiet and 
smoothness. The adaptive variable suspension (AVS) system gives it precise 
handling with minimal body sway. The speed-sensitive power-assisted 
rack-and-pinion steering has a very nice touch: light, but with good feel, 
effortless around town. There is very little, if any, torque steer. The standard 
16-inch wheels come with all-season tyres (215/60R16). <br>
<br>
The brakes are very smooth and effortless, yet sensitive. They require little 
effort to generate good stopping power, but are easy to modulate. The ES300 
comes standard with anti-lock brakes (ABS), which help the driver to maintain 
steering control in an emergency stopping situation; just remember to maintain 
pressure on the brake pedal and to look and steer where you want to go. It also 
comes standard with electronic brake-force distribution (EBD), which distributes 
braking force evenly to the tires with the best grip for excellent stability and 
braking performance. Brake Assist System provides enhanced security, as it helps 
the driver maintain maximum braking force even when he or she makes the mistake 
of relaxing pressure on the brake pedal when the system detects an emergency 
stopping situation. <br>
<br>
<b>Safety </b><br>
As always, safety was a top priority when designing the new ES300. Standard 
safety equipment includes: Global Outstanding Assessment (GOA) body, 8 airbags 
for improved protection - dual front airbags, seat-mounted side-impact airbags, 
and front and rear side-curtain airbags. (Curtain airbags are very important for 
their ability to help provide head protection.) It comes with three-point seat 
belts for all five seating positions; be sure to wear them as they are your 
first line of defence in a crash. <br>
<br>
When it designed the ES, Lexus set out to make it one of the safest cars in the 
class. The passenger compartment is reinforced in critical areas and front and 
rear crush zones are designed to crumple and help absorb the impact of a crash.<br>
<br>
The front seats are designed to help reduce whiplash. The outboard rear 
seatbelts are fitted with force limiters and pre-tensioners. To help the driver 
avoid accidents in the first place, the ES300 comes standard with anti-lock disc 
brakes (ABS), electronic brake-force distribution (EBD), brake assist system 
(BAS), and vehicle stability control (VSC) as well as traction control (TRC) to 
help you control traction loss in turns. <br>
<br>
<b>The finishing line </b><br>
The Lexus ES300 operates in that middle ground marketers refer to with terms 
such as 'entry luxury.' Like other cars in this class, the ES300 is more 
luxurious than the top trim levels of mid-size sedans. The ES delivers a smooth, 
refined ride. Its engine and transmission are smooth and its brakes are smooth. 
The cabin is very quiet. Lexus quality and attention to detail pervade. If you 
want a smooth and quiet mid-size luxury sedan, you cannot do much better in this 
price range than the Lexus ES300. <br>
<br>
<i>Road test courtesy Saud Bahwan Automotive, the sole representatives of Lexus 
vehicles in Oman. </i> </font></p>

