<p align="justify"><font face="Verdana"><font size="4" color="#FF0000">Play Your 
Cards Right<br>
</font><font color="#FF0000" size="1">&nbsp;</font><font size="4" color="#FF0000"><br>
</font></font><b><font face="Verdana" size="1">There is no denying that plastic 
cards are controlling our lives, be it for making a phone call, paying the 
school fee or holidaying. Janaki Raghavan presents a round-up of the various 
types of cards that have made life a breeze</font></b></p>

<p align="justify"><b><font face="Verdana" size="2" color="#FF0000">
<img border="0" src="im23.jpg" align="left" hspace="5" width="150" height="195">Privileges 
galore</font></b><font face="Verdana" size="1"><br>
<b>Credit cardholder spending has grown astronomically in the Arab world over 
the last five years, underscoring a growing preference for credit as a payment 
alternative by consumers</b><br>
<br>
Although there is an entire spectrum of convenience cards Ð debit cards, credit 
cards, phone cards, smart cards, fuel cards, privilege cards etc. to make 
parting with money that much more fun and easy, the credit cards, which 
pioneered the card revolution top the popularity charts of plastic cards. <br>
<br>
<b>Great leap forward </b><br>
Credit cardholder spending has grown astronomically in the Arab world over the 
last five years, underscoring a growing preference for credit as a payment 
alternative by consumers. At the end of June 2002, Visa International reported 
an impressive 26 per cent increase in annual cardholder spending for its Middle 
East division. <br>
<br>
The surge in the region's retail sales across a broad range of products has been 
chiefly instrumental in the increased off-take of credit card volumes. <br>
<br>
Evidently, the emergence of the credit card industry in the Arab world is a 
natural development of the financial markets that is taking place in the region. 
With credit cards being aggressively marketed by banks, more and more people are 
finding them a convenient way of transacting business. <br>
<br>
The trend is apparent in the number of banks that choose to issue credit cards 
and the slew of businesses that currently accept credit as payment. Visa boasts 
roughly 140 member banks with 6.63 million cardholders in the Gulf and Levant 
region. Rival MasterCard offers 112,000 merchant locations in the Middle East 
and North Africa led by Saudi Arabia and the UAE. The company claims more than 
four million cardholders across the Arab world. <br>
<br>
&quot;The Middle East is one of the fastest growing markets in Visa's Central Europe, 
Middle East and Africa (CEMEA) region in terms of card penetration as well as 
performance. Credit and debit cards are more popular than ever before and Visa 
continues to see double-digit growth - proving that credit cards are becoming 
the preferred way to pay for goods and services,&quot; says an industry expert. <br>
<br>
Local banks in the region are taking competition head-on by innovating 
specialised interest-free Islamic products, in addition to extending standard 
services. For instance, in September 2002, ABC Islamic Bank launched the first 
Islamic credit card dubbed Al Buraq. Similarly, Bahrain-based Shamil Bank now 
issues the Al Ruban MasterCard that claims a Shariah compliant credit card 
structure. <br>
<br>
<b>Giving due credit </b><br>
It is estimated that the current credit card numbers in Oman are roughly in the 
range of 75,000. &quot;Credit card business is a high growth area with most banks 
focussing on this product,&quot; says James Moses, Head, Card Center, National Bank 
of Oman (NBO). <br>
<br>
Hatem Ali Abdul-Fattah, Marketing Manager, Smart Cards Systems, Oman Arab Bank, 
states &quot;Oman's debit card market is roughly 1/2mn but only 1 per cent use the 
card for shopping.&quot; <br>
<br>
All the local commercial banks in the Sultanate, which are locked in a feisty 
battle to seize the credit card market, have a slew of card products for every 
pocket and requirement.<br>
<br>
NBO which has recently merged with BankMuscat, offers the NBO MasterCard which 
is co-branded with Oman Air, the NBO Web shopper card and the NBO Visa Card. 
&quot;Cash can be withdrawn at anytime and anywhere in the world. Our cards provide a 
flexible payment plan wherein a customer can pay as little as 5 per cent of the 
amount due. In addition we have an insurance protection that offers accident 
insurance of up to $500,000 (RO192,500),&quot; mentions Moses. <br>
<br>
Moses also adds that the NBO call centre service is available for 
round-the-clock. &quot;NBO is the only bank that has a Money Back plan where credit 
card customers are rewarded for use of cards in either the merchant outlet or 
for withdrawal of cash. Another unique feature is that NBO has a co-branded card 
with Oman Air,&quot; claims Moses. <br>
<br>
&quot;Oman International Bank (OIB) is the market leader in the card business in 
Sultanate with a wide range of products to satisfy various segments of 
customers. OIB is both the issuer of credit/debit cards as well as acquirer of 
credit card transactions. It has the largest network of Point of Sales (POS) 
terminals installed at the merchant's outlets to facilitate the customer to pay 
with his plastic card,&quot; declares Amit Desai, Chief Manager, Card Centre, OIB.
<br>
<br>
OIB's credit card portfolio comprises the Classic Visa Card, Gold Visa Card, 
Standard Master Card and the Gold MasterCard. The bank extends the Revolving 
Credit Cards, which is a buy-now-pay-later facility whereby the cardholder is 
required to pay only 5 per cent of total card outstanding balance and can carry 
forward the remaining. &quot;We also offer two cards for the price of one, i.e., if 
the customer has applied for a Visa Card, a MasterCard is issued free and 
vice-versa. We have interest free credit period of upto 45 days and free 
insurance coverage of upto $ 250,000 (RO96,250). In addition OIB is the only 
bank in Oman which provides the free 'Purchase Protection Plan' coverage which 
entitles the cardholder for a refund of the full amount on any item purchased 
through OIB's credit cards either locally or overseas in the event of it getting 
lost, stolen or damaged, provided the claim is lodged within 90 days from the 
date of purchase,&quot; reveals Desai. <br>
<br>
OIB's cards also give access to free global calling card for all the successful 
applicants. As a value added feature OIB allows the customer to settle his 
outstanding card balance at any of the OIB's branch or pay by 'Phone Bank' from 
anywhere in the world. <br>
<br>
Bank Dhofar Al Omani Al Fransi, which has about 10,000 credit cards in 
circulation, offers 7 types of credit cards Ð The Visa Gold, Visa Classic, Visa 
Electron, MasterCard Platinum, MasterCard Gold and MasterCard Standard and the 
Ecomcard. Bank Dhofar Cards are unique in that they offer insurance coverage on 
flight delay and loss of baggage. <br>
<br>
Oman Arab Bank's (OAB) credit card products include Visa Platinum, Visa Gold, 
Visa Standard and the OAB Smart card. &quot;The Smart Card is positioned as the 
ultimate in safety and convenience. It is based on chip technology with full 
built-in security. As it is used as an offline card with all the parameters 
executed offline, it results in time saving. It is the only electronic payment 
card that can be used to pay government fees, utility bills, fuel stations, 
shopping centres, hospital, dining etc,&quot; mentions Abdul-Fattah. As a convenience 
card the Smart Card offers substantial discounts to users across the market. It 
is also a rewarding card that enables users to win five prizes totalling 
RO20,000 in a monthly raffle. The Smart Card available at RO3 is the lowest 
priced cost card in the market. Both types of Smart Cards Ð the consumer market 
card and the corporate market card have received an overwhelming response 
according to the bank. <br>
<br>
BankMuscat offers a good range of credit cards. MasterCard Platinum, which is 
offered only by invitation extends a high spending limit, personal concierge 
service, global emergency service and helps earn Gulf Air Frequent Flyer miles 
on card usage. The Corporate Purchasing Card - MasterCard, a multifunctional 
financial tool enables companies to expedite their purchasing process by 
optimising the financial supply chain efficiency. With the card, purchases can 
be made directly by the designated cardholder from the supplier and the supplier 
will receive payment from BankMuscat within 48 hours. Visa Gold and Gold 
MasterCard have a worldwide acceptance, offer high spending limit, 100 per cent 
cash advance limit, instant cash worldwide, photocard, zero lost card liability, 
free travel insurance, Gulf Air frequent flyer miles on card usage, emergency 
services and online fraud monitoring. Visa Classic and Standard MasterCard also 
have a worldwide acceptance, and extend 100 per cent cash advance limit, instant 
cash worldwide, photocard, zero lost card liability and free travel insurance. 
The bank also offers co-branded shopping cards with Al Fair and Al Burj. Safe 
Card, is a virtual card 100 per cent secured for Internet use. BankMuscat's Visa 
Electron can be used worldwide both at Point Of Sale and ATMs. <br>
<br>
<b>Privileges galore </b><br>
Privilege cards are fast gaining recognition as an effective tool for retaining 
a captive customer base. <br>
<br>
The City Plaza Privilege card offered by the Landmark group was launched in Oman 
in June 2004. &quot;We have similar loyalty card programme in other territories too. 
In UAE we have over 100,000 privilege card holders. We constantly look at 
rewarding our customers and providing them value addition. It is our way of 
saying thank you by giving them special offers, states Santosh K Pai, General 
Manager, Landmark Oman. <br>
<br>
The City Plaza Privilege Card can be obtained by paying a processing fee of 
RO2.5 or a free-of-cost card can be earned upon making a total purchase of RO25 
in one day, at any one of the Landmark stores. &quot;A card member can earn 1 point 
worth RO0.100 for every RO1 spent and these privilege points will be converted 
to Shopping Vouchers issued in multiples of RO5,&quot; elucidates Pai. <br>
<br>
<b>A rewarding experience</b><br>
With automobile manufacturers becoming increasingly aware of the importance of 
fostering customer loyalty, a host of auto privilege cards have been unleashed 
in the market. <br>
<br>
Lexus offers its customers in Oman the Lexus Prestige Club Card. &quot;The improved 
new Prestige Club Card will cater to the Lexus owner's lifestyle demands in new 
ways,&quot; says a Lexus spokesperson. <br>
<br>
With the Lexus Prestige Club Card, Lexus owners will be able to avail 24-hour 
on-road assistance, special UAE assistance hotline, emergency fuel delivery, 
special 'Meet and Greet' facility at Seeb International Airport, exclusive 
shopping discounts at over 300 premium outlets in the country and fuel and 
credit facility from Omanoil. <br>
<br>
The Prestige Club Card also extends life insurance cover of up to RO5000 and 
provides assistance with documentation. All new Lexus customers are entitled to 
the Prestige Club Card facilities while existing customers will have to pay a 
nominal fee to avail of the membership. <br>
<br>
The new Toyota Advantage Card offers 23 exclusive benefits and privileges to 
Toyota and Daihatsu owners in Oman. The benefit package includes vehicle related 
assistance (24/7 hotline facility, road-side assistance including mechanical 
repairs and renewal of registration etc), financial assistance (credit 
facilities, payment of bills, fuel credit) and personal assistance (airport 
assistance, relay of passengers, hotel accommodation). In addition the new card 
offers discounts and privileges at a variety of member establishments in over 
400 outlets in Oman and UAE including health clubs, pharmacies, restaurants, 
bakeries and computer shops. Toyota/Daihatsu owners can get a personalised Club 
Card from any of the Toyota showrooms. <br>
<br>
The Ford Privilege Club Card offers exclusive shopping discounts on a wide range 
of products from several leading outlets across the country. It also entitles 
the holder to a 24-hour on-road assistance from AAA, which provides an extensive 
range of emergency services. A Dubai hotline assistance is also a part of the 
package. This is part of Ford's continuing endeavours to delight its customers,&quot; 
says a Ford spokesman. <br>
<br>
<b>Fuel smart</b><br>
Fuel Credit Cards are becoming increasingly popular in the market by proving to 
be an effective business tool to track fuel consumption and optimise fuel costs. 
The host of other benefits that they offer have also increased their popularity 
quotient. <br>
<br>
&quot;Shell Oman offers a variety of cards to suit individual and fleet customers. 
Launched in 1997, Shell Cards were the first electronic purchase system for fuel 
and lubricants to be introduced in Oman. Accepted at all Shell outlets, Shell 
Cards offer a secure and flexible method of managing fuel consumption and 
costs,&quot; says a spokesperson from Shell Oman. <br>
<br>
Shell Oman later launched the Smart Card - an electronic chip based card to 
ensure better control and easy retrieval of required information. Shell offers 
different types of cards: Gold, Fleet, Fuel and Project. &quot;Usability of these 
cards is tailor-made to help our customers manage their business more 
efficiently. Shell Fleet card is the most convenient way to save operating 
costs, time and paperwork and monitor fuel expenditure,&quot; claims the company 
spokesman. <br>
<br>
While the Gold Card is personalised for the executive, the Fleet Card is a 
vehicle specific card that provides the owner detailed analyses on the distance 
travelled, fuel purchase pattern and fuel consumption. The Fuel Card on the 
other hand is useful in case of emergencies, rented cars, jerry can filling, 
storage tanks, site equipment filling, etc. </font></p>

