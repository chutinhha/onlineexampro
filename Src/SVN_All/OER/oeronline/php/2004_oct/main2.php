<p align="justify"><font face="Verdana" size="4" color="#FF0000">Mergers and 
managers don't mix<br>
</font><b><font face="Verdana" size="1"><br>
Mergers and acquisitions dominate business headlines. Shareholders win, 
employees suffer. Is there any way to do it better?</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im29.jpg" align="left" width="300" height="235">Mega-mergers like BP and Amoco, 
Mercedes-Benz and Chrysler or mini-mergers of European rivals seem to have one 
thing in common - they create a working hell for the people inside, particular 
at middle management and below. Too often - months after the first euphoric 
announcement - we see less than enthusiastic headlines in the media, usually 
linked to job cuts, departing senior executives and crumbling share prices. So 
what's going wrong? <br>
<br>
&quot;Between two-thirds and three-quarters of all mergers and acquisitions (M&amp;As) 
around the world are failures because 2+2 end up equalling 3 rather than 5,&quot; 
answers Cary Cooper, a mergers specialist at the University of Manchester. He 
adds, &quot;mergers fail, because the culture of the companies isn't looked at - if 
one company has a power culture and the other is more social, it just won't 
work.&quot; <br>
<br>
<b>The human factor </b><br>
Organisational consultant Mark Thomas agrees, &quot;As many as 70 per cent of M&amp;As 
result in financial disappointment and most of that is based on soft people 
issues and hard business issues colliding. Add to that a company's inability to 
spend time on post-acquisition actions and you have a potential disaster.&quot; 
Silvio Conforti, an M&amp;A specialist with the Union Bank of Switzerland in Zurich 
adds, &quot;It is true that the consideration of the people affected is limited. M&amp;A 
transactions are normally decided, planned and executed by a small group of 
executives (who are likely, but not necessarily, to benefit from them). The rest 
of the organisation is the object, not the subject of the integration process.&quot;<br>
<br>
Conforti also explains why M&amp;As usually come as a complete surprise to those 
inside the organisation, &quot;In principle a human resource audit would be a good 
thing, but in practice this is not often possible in view of the need to 
maintain confidentiality.&quot; <br>
<br>
Nick Winkfield, president of Wirthlin Europe, a market research firm, supports 
that view, &quot;Of course there should be a human resources audit, but it is often 
difficult without disclosing an interest.&quot; <br>
<br>
<b>Internal communications </b><br>
All the same, once the merger is happening, it is important to get two things 
right: move fast to stop rumours (a corporate communications vacuum will be 
filled by employee-generated speculation and will be taken as the truth in the 
absence of any other news) and treat those who lose out in the fairest and most 
open way possible. It is getting these two things wrong that seems to bring the 
most grief to mergers. <br>
<br>
Cary Cooper comments cynically, &quot;Usually, managing mergers is done very badly, 
which makes the stress even worse for those involved. Most companies have very 
poor communication systems. They usually say things like, Ôthere will be no job 
losses,' but there always are. It is wholly unbelievable, but lies get told to 
stop the good people from leaving.&quot; Peter Dixon, a merger specialist with 
consultants Braxton Associates supports Cooper's view and adds, &quot;Lack of 
information, no clear direction and confusing messages, all boil down to 
uncertainty, which is destructive. It is really very important to be clear and 
consistent even if the messages may not always be positive for everyone.&quot; <br>
<br>
This is particularly true in dealing with people. &quot;If the worst is to happen to 
people,&quot; says Dixon, &quot;then proceed fairly and swiftly to avoid uncertainty and 
the constant rumour-mongering and demotivation which can rapidly sweep an 
organisation.&quot; And he warns, &quot;If you don't do this, it will usually be the 
people you want to stay who will leave.&quot; <br>
<br>
<b>Leadership and speed </b><br>
Winkfield adds, &quot;If I were in charge of a merger the two things I would 
concentrate on are leadership and speed. For most employees - and most of all 
for middle managers - mergers are crises, and in a time of crisis people look 
for leadership. But the good ones won't have to look for long and you can say 
goodbye to them at the first hint of trouble.&quot; Even those who stay are not going 
to be performing at their best. &quot;Worry about what your staff don't tell you,&quot; 
advises Winkfield, &quot;they are anxious and insecure and in this frame of mind they 
are likely to be inefficient, excessively risk-averse and lousy ambassadors for 
the company.&quot; <br>
<br>
Much of that trouble comes from a total failure to implement the processes that 
will make the merger or acquisition successful, &quot;Often when a deal is 
engineered,&quot; suggests Mark Thomas, &quot;the only people present are the leaders and 
huge numbers of financiers and corporate lawyers. None of these people have the 
detailed knowledge, interest or incentive to work through potential people 
issues.&quot; <br>
<br>
This spotlights another problem: people at the top might think that everyone 
below is getting along just fine, just because they are. Winkfield: &quot;In newly 
merged companies, the top management team may rapidly integrate. Equally at the 
operational level, task forces and teams are formed, forcing co-operation and an 
alignment of values: either people fit in or they go.&quot; <br>
<br>
&quot;But for many middle managers, there is less opportunity to work with their new 
colleagues on a day-to-day basis and it is at this level - and below - that the 
challenge of cultural alignment is usually the greatest.&quot; <br>
<br>
The other merger issue of course is the ego-driven CEO, who, as Cary Cooper 
says, &quot;does it for personal aggrandissement, usually because he isn't clever 
enough to grow the organisation organically.&quot; Or, as Winkfield observes, &quot;there 
comes a sense of 'been there, done that', so after a few years as CEO there's 
nothing else left but to go for a big deal.&quot; <br>
<br>
But M&amp;As show no sign of slowing down. As Peter Dixon predicts, &quot;It's best to 
view M&amp;As as a natural part of the business landscape - not as some irrational 
phenomenon.&quot; And he offers this advice: &quot;The chances are very high that most 
people in business will be involved in M&amp;As several times in their careers, so 
it is important to be prepared.&quot; </font></p>

