<p align="justify"><font size="4" color="#FF0000" face="Verdana">The New Radio 
and TV Law</font><font face="Verdana"><font size="4" color="#FF0000"><br>
</font><font color="#FF0000" size="1">&nbsp;</font><font size="4" color="#FF0000"><br>
</font></font><b><font face="Verdana" size="1">Michael Earley examines the 
significant portions of the new law allowing the establishment of privately 
owned and operated radio and TV stations in the country</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im25.jpg" align="left" hspace="5" width="74" height="89">Radio and television broadcasts 
within the Sultanate of Oman have been the exclusive domain of the government so 
far. Natural or juristic persons were not permitted to own and operate a radio 
or television company ('Station'). However, the recent Sultani Decree 95/2004, 
promulgating the Private Radio &amp; Television Stations Law (PRTSL) is set to 
change the Sultanate's broadcasting horizon by allowing the establishment of 
privately owned and operated radio and television Stations in Oman. This column 
focuses on the more significant portions of the new law, including the new Radio 
and TV Stations Committee ('Committee') and the licensing requirements for 
establishing private Stations. <br>
<br>
<b>The Radio &amp; TV Stations Committee </b><br>
The PRTSL provides for the establishment of a Committee to oversee the 
activities of the Stations. According to Article 12 of the PRTSL, the Committee 
shall be comprised of the following members:</font><font face="Marlett" size="1"><br>
h</font><font face="Verdana" size="1"> The Undersecretary of the Ministry of 
Information: Chairman. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
Undersecretary for Cultural Affairs at the Ministry of Heritage and Culture: 
Deputy Chairman. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
Undersecretary of the Ministry of Tourism. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
Undersecretary for Communications at the Ministry of Transport and 
Communications. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> The 
President of the Oman Chamber of Commerce &amp; Industry. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> Two 
public personalities to be appointed by a decision from the Council of Ministers 
upon a nomination from the Minister of Information. <br>
<br>
Article 13 of the PRTSL grants the Committee substantial powers with respect to 
regulating Stations. Specifically, the Committee shall be responsible for the 
following:<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Establishing the general framework for information policies to ensure the 
Stations comply with the needs of society. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Approving licences for the Stations and approving the provision of associated 
radio and TV services after reviewing the licence applications. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Ensuring that the Stations are abiding by the terms of their respective licences.
<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Approving any amendments to the Stations' licence and services. <br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
Banning radio or TV broadcasts for a period not exceeding three days if the 
Committee reaches a justified reason that a Station has violated provisions of 
the PRTSL. <br>
<br>
Any exercise of the powers outlined above must be through a Committee 
resolution. Resolutions of the Committee may only be valid if such resolutions 
are passed by an absolute majority of the Committee Members, and all resolutions 
must be approved by the Minister of Information prior to taking effect. <br>
<br>
Although the PRTSL grants to the Committee the power to ban the broadcasts of a 
particular Station for a period not exceeding three days, it does not detail the 
process in which the Committee makes such a decision. Furthermore, it is unclear 
from the PRTSL whether there is a procedure through which the affected Station 
may make an appeal.<br>
<br>
<b>Licence requirements</b><br>
Article 20 of the PRTSL provides that a private radio or TV Station may only be 
established in Oman or its territorial waters after first obtaining a licence to 
operate. The only corporate form available to Stations desiring to obtain such a 
licence is that of a closed joint stock company (SAOC), wholly owned by Omani 
nationals. Sultani Decree 04/74 promulgating the Commercial Companies Law (CCL) 
requires an SAOC to have at least three natural or juristic shareholders, and a 
minimum capital of RO500,000. However, unlike a conventional SAOC, Stations 
licensed under the PRTSL may not transfer or otherwise dispose of their shares 
to a non-Omani party. <br>
<br>
The licence application submitted to the Committee must contain the particulars 
of the Station, including the type of radio or TV service provided, the place of 
broadcasting, the areas covered by the broadcast, the means of broadcasting, the 
target audience of the broadcasts, and the broadcasting hours and timings. The 
application must also be accompanied by an undertaking by the Station that it 
will implement the directives of the Committee. <br>
<br>
The licence fees are to be determined by the Committee working in conjunction 
with the Ministry of Finance and will depend on the broadcast classifications 
(detailed below). The Committee shall reach a decision within four months from 
the date of submission of the application. Factors the Committee considers in 
deciding whether to approve a licence application are manpower and equipment 
requirements of the Station, availability of channels pursuant to international 
agreements and existing laws, technical equipment requirements, financial 
solvency, and an undertaking to employ Omani nationals. <br>
<br>
Licence applications are evaluated in the order in which they were received. If 
a licence application is rejected, the Committee must provide reasons. Unlike 
decisions by the Committee to ban broadcasts, Article 28 of the PRTSL 
specifically provides that an applicant may appeal to the Minister of 
Information the Committee decision to deny its licence application, provided 
that such appeal is made within 30 days. <br>
<br>
Once approved, a PRTSL licence is valid for a period of ten years. The licensed 
Station is required to start broadcasting within one year. If not the Station 
may request a further year extension. <br>
<br>
If the Station has not started broadcasting by the expiration of the applicable 
period, the licence gets cancelled. Furthermore, if the services provided by a 
Station stop for a continuous period of six months within a year without a 
reason acceptable, the Station's licence shall be cancelled. <br>
<br>
A licence issued under the PRTSL may not be transferred. The licence may be 
renewed by submitting an application for renewal to the Committee one year prior 
to the expiration. Factors the Committee will consider in deciding whether to 
renew a licence include whether the Station has met the required percentage of 
Omani nationals working in technical positions within the Station.<br>
<br>
<b>Broadcast classifications </b><br>
The PRTSL categorises broadcasts by audience/area of broadcast. Regarding the 
area of broadcast classification, Stations may be categorised as one of the 
following:<br>
<br>
(1) Stations broadcasting to all areas, <br>
(2) Stations broadcasting to limited areas,<br>
(3) Stations whose coded broadcasts only reach subscribers, <br>
(4) Satellite Stations broadcasting beyond the boundaries of the Sultanate, or
<br>
(5) Stations that receive materials from external stations under agreements and 
rebroadcast such material within the Sultanate. <br>
<br>
The PRTSL also categorises Stations based on broadcast material. A Station may 
either be: <br>
(1) A public service Station , or<br>
(2) A specialised service Station only broadcasting certain programmes such as 
news, sports, movies, or other specific content. <br>
<br>
A Station's categorisation will determine the licence fees and charges payable.
<br>
<br>
<b>Station management</b><br>
The professional conduct of the Stations' employees is governed by the 
Publication Law. Once a Station has been established, the Station must appoint a 
manager with experience to represent it before the courts. The language of the 
Stations' broadcasts must be in either English or Arabic. If the use of a 
different language is envisaged, prior approval must be obtained from the 
Council of Ministers. <br>
<br>
<b>Future broadcasting</b> <br>
As the existing regulations currently governing public radio and television will 
also apply to private Stations, it is too early to tell what impact the PRTSL 
will have on Oman Radio and Television. However, the proliferation of privately 
owned Stations should expand Oman's broadcasting variety and create lucrative 
advertising opportunities for Stations. The author is an attorney with Denton 
Wilde Sapte's office in Oman. </font></p>

