                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">ON THE 
            PROWL</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>Finally coming 
            of age, Oman's banking sector is consolidating its position to 
            attain the muscle to compete in the region. OER looks at the 
            country's banking and finance industry through the prism of the 
            latest merger-BankMuscat and NBO, the two largest banks in the 
            Sultanate-and evaluates if it is ready to prey in the regional 
            jungle </b></font><b> 





                  <font face="Verdana" size="1"><br>
            <br>
                  </font>





                  <font face="Verdana" size="2" color="#FF0000">
            <img border="0" src="tiger1.jpg" align="left" hspace="5" width="200" height="270">Might 
            is Right</font><font face="Verdana" size="1"><br>
            Will the merger of BankMuscat and NBO give the country's banking 
            sector enough muscle to become a serious contender in the region?</font></b></p>
            <p align="justify"><font face="Verdana" size="1">Call it the 'big is 
            beautiful' syndrome. With the country's two largest banking 
            entities-BankMuscat and National Bank of Oman (NBO)-succumbing to 
            the temptation of joining hands to acquire the muscle required to 
            play among the big boys of the region, mergers and acquisitions 
            (M&amp;As) in the Sultanate's banking industry seem to have become the 
            rule rather than an exception. <br>
            <br>
            The consolidation drive in the banking sector of the country-which 
            in the past has seen a handful of heavyweight banks disappear 
            overnight-is slowly but surely providing the country's banks with 
            the assets base and the financial clout to compete with the regional 
            players. After three years of a rollercoaster ride, Oman's banking 
            sector may finally be ready to take on the regional challenge. <br>
            <br>
            With the proposed merger of the top two Omani banks already 
            receiving the Capital Markets Authority's (CMA) and Central Bank of 
            Oman's (CBO) in-principle approval, the face of commercial and 
            retail banking in the Sultanate is set to change significantly. In a 
            way, consolidation was both indispensable as well as inevitable. The 
            groundwork for the current spate of M&amp;As was laid in 2002, when 
            arrangements for a GCC common market began in earnest. <br>
            <br>
            As expected, the move has led to a consolidation within the Omani 
            banking sector before the influx of foreign banks-especially from 
            Bahrain and the UAE, the so-called financial hubs of the 
            region-begins. As per BankMuscat's announcement, the merger will be 
            effective January 1, 2005. The merger with NBO will also allow 
            BankMuscat to find a much-awaited entry into the UAE market, through 
            NBO's operations in Abu Dhabi. This is seen to be a key step forward 
            in BankMuscat's realisation of its regional ambitions. <br>
            <br>
            <b>Merger mathematics </b><br>
            While the combined assets base of the merged entity works out to 
            RO2.37 billion (2003 results), this figure is expected to be higher 
            in lieu of the banks' growth in 2004, as well as the fact that, in 
            the case of a healthy merger, the whole is always more than the sum 
            of its parts. There are obvious synergies between both the banks, 
            and the management of the merged entity is expected to fully utilise 
            these to action operational cost rationalisations. <br>
            <br>
            To be sure, according to a statement released by BankMuscat, ''the 
            merged bank is expected to be a $6.65 billion (RO2.56 billion) 
            entity, well poised to make its presence felt across the region and 
            take on the increased level of international competition that is 
            expected in the Sultanate, post the implementation of the Basel II 
            Accord and the WTO directives, a couple of years from now.'' <br>
            <br>
            That, in a nutshell, also sums up the two most important reasons for 
            the merger: defending home turf in the face of imminent competition 
            from global players, and attaining greater muscle to satisfy its 
            regional ambitions. However, while this move does make BankMuscat-the 
            largest player in the local market-even bigger, it alone may not 
            suffice to fulfil the bank's regional game plan. As Sheikh 
            AbdulMalik bin Abdullah Al Khalili, the chairman of BankMuscat, said 
            in an exclusive interview with OER, ''We will continue to look out 
            for opportunities of growth in the regional market.'' <br>
            <br>
            As per BankMuscat's initial announcement, in exchange for every 100 
            ordinary shares of NBO held on the record date of the merger 
            transaction, NBO shareholders will receive: <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">In 
            respect of the first 85 shares of NBO: 37 and 7/9 ordinary shares of 
            BankMuscat (at an exchange ratio of 2.25:1), which shall be 
            identical to the existing issued ordinary shares of BankMuscat, 
            except for any normal cash dividend payable by BankMuscat for the 
            year ended 2004. <br>
            </font><font size="1" face="Marlett">h</font><font face="Verdana" size="1">In 
            respect of the remaining 15 shares of NBO: BankMuscat will be 
            issuing 15 subordinated loan bonds at an interest rate of 6.25 per 
            cent per year, payable half yearly, in arrears. Each bond will carry 
            a nominal face value of RO2.750 payable at redemption of the 
            maturity period of 5 years and 1 month. The bonds will also be 
            listed and may subsequently be traded on the Muscat Securities 
            Market (MSM). <br>
            <br>
            For the purpose of arriving at the above consideration, NBO's net 
            worth-which stood at RO100.281 million on June 30, 2004-has been 
            considered at RO100 million as at December 31, 2004. <br>
            <br>
            BankMuscat has rocketed to the top of the country's market through a 
            series of mergers in the past several years, with Commercial Bank of 
            Oman (ComBank), the Bank of Oman, Bahrain, and Kuwait (BOBK), 
            Industrial Bank of Oman (IBO), and now NBO. The bank has proclaimed 
            that it is on the lookout for opportunities to further strengthen 
            its regional base. The largest bank in Oman, BankMuscat moved in 
            that direction in December 2001, when it acquired the Bahrain 
            operations of ABN AMRO bank. Since then, it has managed to maintain 
            a robust growth through a spate of M&amp;As, including that of strategic 
            stake in Centurion Bank in India. <br>
            <br>
            While the board of directors of both BankMuscat and NBO have 
            resolved to go ahead, the merger is subject to approval by 
            shareholders of both the banks, which is expected to go through at 
            an Extraordinary General Meeting (EGM) to be convened in November 
            this year.<br>
            <br>
            Once the other technicalities and formalities of the merger are 
            taken care of-due approval and registration by the Ministry of 
            Commerce &amp; Industry of the Sultanate in accordance with the 
            Commercial Companies Law-the reinforced BankMuscat is expected to 
            have a combined assets base big enough to propel the resultant 
            entity into the first half among the Top 50 GCC banks. BankMuscat 
            and NBO ranked No. 27 and No. 37, respectively, based on their 2003 
            results<br>
            <br>
            <b>Merger mania </b><br>
            If one weren't careful, it could be easy to get confused by the 
            surfeit of past M&amp;As in the Omani banking sector, which seems 
            nothing less than a maze. Every existing Omani bank in the Sultanate 
            has a long history of M&amp;As through which it came into being. <br>
            <br>
            The other commercial Omani banks in the country were also a result 
            of a string of M&amp;As - whether it be BankDhofar, which was formed 
            after acquiring the business of the Muscat branch of the French bank 
            Banque Paribas; or Oman International Bank (OIB), which came into 
            existence on January 1, 1984, when Arab African International Bank 
            sold its 60 per cent shareholding to H.E. Dr Omar Bin Abdul Muniem 
            Al Zawawi. Even BankMuscat was reconstituted in 1993 as a public 
            joint stock company by the voluntary merger of two established banks 
            - Bank of Muscat and Al Bank Al Ahli Al Omani, which were operating 
            in Oman since 1982 and 1976, respectively. <br>
            <br>
            The last significant merger involving BankMuscat was that with 
            ComBank in 2000. That merger assumed a lot of importance in the 
            corporate history of the country since it happened at a time when 
            several merger plans got shot down for different reasons. The 
            BankMuscat-ComBank merger put an end to the corporate world's long 
            wait for a serious merger, acquisition or takeover since the merger 
            of ComBank with the Bank of Oman, Bahrain and Kuwait (BOBK) in 1998, 
            to form one of the largest capitalised corporate entities in the 
            country with an equity capital of RO55.4 million.<br>
            <br>
            Interestingly, the merger of ComBank and BOBK too was preceded by a 
            couple of buy-outs and mergers. In fact, the seed of ComBank can be 
            traced back to the ANZ Grindlays Bank, which was established in the 
            Sultanate way back in 1969. As of 1996, Grindlays had two branches 
            in Oman, until the Omani operations of Grindlays Bank were bought 
            out by a group of investors under the leadership of Dr Mohamed Musa 
            Al Yousef to set up a new bank, Oman Savings &amp; Finance Bank (OSFB).
            <br>
            <br>
            ComBank was established in 1976, and in 1993, it acquired three 
            branches of Standard Chartered Bank (StanChart). However, in 1997, 
            OSFB was merged with ComBank and took the name of the latter. Again, 
            in 1998, BOBK, which was established in 1973, officially merged with 
            Commercial Bank of Oman. <br>
            <br>
            Interestingly, the rumours about OIB and BankMuscat getting merged 
            made big brouhaha in the Sultanate's financial market in the late 
            1990s till both the banks denied the rumours summarily. The 
            Industrial Bank of Oman was merged with BankMuscat in 2001. This was 
            primarily intended to augment BankMuscat's capital adequacy 
            requirements for future expansion plans. Their asset size of $100 
            million (RO38.46 million) was of no significant relevance to the 
            merger, but the capital of $65 million (RO25 million) bolstered 
            BankMuscat's equity base. <br>
            <br>
            With the entry of Oman into the World Trade Organisation (WTO), 
            everybody believes that the only way to combat the impending 
            competition is to become big and strong. That may also be the 
            predominant reason behind the government's keenness to see mergers 
            take place in the country. The MOCI even went to the extent of 
            appointing an agency to study the feasibility of mergers of 
            companies, which saw the merger of three brokerage entities-Al 
            Mawarid Securities Co., Al Shalman Securities Co. and the Investment 
            Banking Division of Oman &amp; Emirates Investment Holding Co.-into 
            Financial Corporation (Fincorp) last year. <br>
            <br>
            Last year also saw the acquisition of Majan International Bank by 
            Bank Dhofar Al Omani Al Fransi, which has since undergone a 
            re-branding exercise and is now known as BankDhofar. <br>
            <br>
            <b>Industry outlook </b><br>
            In the last few years, the banking industry in Oman was 
            significantly hit due to bad-loan pains. However, the Sultanate's 
            financial sector has now undergone a metamorphosis, and its future 
            looks positive and promising. According to a recent report by the 
            Economic Research &amp; Statistics Department of the CBO, the country's 
            apex banking organisation, the monetary and banking scenario in the 
            country continued to support growth with comfortable liquidity for 
            banks, softening interest rates and healthy levels of liquidity in 
            the stock market. <br>
            <br>
            Last year saw new entrants, M&amp;As, consolidations and technological 
            changes impacting the sector for the better. This year, industry 
            experts are already hailing the proposed merger of BankMuscat and 
            NBO as the 'news of the year', as it signifies the coming of age of 
            the Omani banking sector, with the merged entity expected to have a 
            mass that will make the regional players sit up and take notice.<br>
            <br>
            The vitality of Oman's banking sector was confirmed when it posted 
            strong results for the first half of 2004. The four local banks 
            listed on the MSM posted a whopping 135.5 per cent jump in combined 
            net profits over the same period for 2003. Of course, last year's 
            results were subdued mainly on account of a heavy loss incurred by 
            NBO. <br>
            <br>
            But after some difficult years, most Omani banks are reporting a 
            significant upturn following efforts to improve discipline. 
            Meanwhile, the stock market too is showing signs of growth and 
            project finance deals are picking up. The country's banking sector 
            today consists of 16 local and foreign institutions, four 
            specialised banks and three investment firms. <br>
            <br>
            Omani banks have reported significantly improved results in H1 2004, 
            and they are expected to consolidate this situation in the latter 
            half too. This is good news for the Sultanate following the volatile 
            business conditions and embarrassing payment failures that dented 
            confidence in 2001-02, when the local equities market declined 
            sharply and banking scandals undermined some of Oman's oldest and 
            best-known institutions. Since then, the authorities have been 
            working to tighten up financial sector governance, whose failings 
            have become only too apparent through episodes such as the collapse 
            of the high-flying Ali Redha Trading Group. <br>
            <br>
            The CBO now demands capital adequacy levels above 12 per 
            cent-compared to Basel I's 8 per cent provisioning-and has 
            introduced tougher fines for institutions that breach its rules. 
            Quarterly inspections of accounts by CBO inspectors have been 
            initiated to increase discipline and transparency and, as part of a 
            wider tightening of the MSM's lending habits, only 5 per cent of an 
            Omani bank's total lending may now be extended to non-national 
            borrowers. This move is a clear reflection of the troubles that hit 
            many local institutions during the boom time, when CBO's limit was 
            15 per cent of total lending to any single foreign client. <br>
            <br>
            After a flamboyant period when its financial institutions sought to 
            cut loose, with ventures abroad and new products to tempt new 
            investors, the Sultanate's innate conservatism is being reflected in 
            the ever more cautious approach of its regulators and bank 
            management. Thus, while a buoyant oil price has helped stimulate the 
            domestic economy, breathing new life into bank deposits and the MSM, 
            caution remains the watchword. <br>
            <br>
            <b>Regional perspective </b><br>
            An analysis of the statistics of the region's top banks reveals that 
            size does indeed matter. The Gulf's big banks continue to get 
            bigger, posting stellar performances for financial year 2003. On the 
            back of a strong regional economy boosted by high oil prices, rising 
            stock markets, a booming property sector and improved optimism, 
            banks in the GCC recorded good performances in 2003. Although 
            margins remain tight, this was more than offset by higher loan 
            volumes and rising fee incomes. The strong performance in 2003 
            followed two years of comparatively weak results. <br>
            <br>
            Reflecting a better operating environment, the Top 50 GCC banks saw 
            net profits rise by a massive 23 per cent in 2003 on an aggregated 
            basis against just 2 per cent in 2002 and 3 per cent in 2003. Of the 
            top 50 banks in the GCC, the largest 20 hold 76 per cent of the 
            assets of the combined asset base, 72 per cent of capital and 79 per 
            cent of consolidated net profits. Figures from the largest ten GCC 
            banks indicate that they represent 50 per cent of assets, 46 per 
            cent of capital and 50 per cent of profits. The figures indicate the 
            advantage of size. <br>
            <br>
            <b>Saudi Arabia </b><br>
            Saudi Arabian banks continue to dominate the sector, with seven 
            institutions in the Top 20 rankings. Saudi banks generally display 
            solid overall financials with high levels of capital and liquidity, 
            as well as very good returns. National Commercial Bank (NCB) remains 
            the region's largest bank based on capital, assets and net profits. 
            The bank's net profit rose by 23 per cent to $798 million (RO307 
            million) in 2003 and recorded the highest ROE among the Top 50 GCC 
            banks with a return of 28.9 per cent. The main factors behind the 
            improvement included an 11 per cent increase in net special 
            commission income as a result of expansion in lending activities; 
            $165.3 million (RO63.6 million) in doubtful loan recoveries, which 
            resulted in a substantial drop in the provision expense for possible 
            credit losses compared with 2002; increased trading income and 
            capital gains; and improved operating efficiency. <br>
            <br>
            The net profit of Saudi American Bank (SAMBA), the second biggest 
            Saudi bank in terms of capital in the GCC, again slipped in 2003, 
            falling by 23 per cent to $384 million (RO147.7 million). In 2002, 
            its net profit fell by 17 per cent. This drop reflected 
            significantly higher provision charges. The bank's ROE fell to 16.2 
            per cent in 2003 from 20.9 per cent in 2002 and 26.8 per cent in 
            2001. SAMBA took the decision to enhance credit provisions, 
            utilising $165.8 million (RO63.8 million) as additional reserves in 
            2003 compared to $26.9 million (RO10.3 million) in 2002. In 
            September 2003, an agreement with Citigroup was concluded to 
            complete the transfer of SAMBA to local management. <br>
            <br>
            Saudi's Riyad Bank, ranked third overall based on capital, again 
            performed well in 2003 with net profits rising by 12 per cent to 
            $425 million (RO163.5 million). It has one of the highest capital 
            ratios in the region, which affects its ROE (still a good 18 per 
            cent in 2003, and its return on assets (ROA) ratio is high at 2.2 
            per cent - both ratios rose in 2003). Its size and ownership (SAMA 
            and the Ministry of Finance) make it a major force in the financing 
            of large government or quasi-government projects.<br>
            <br>
            Al Rajhi Banking &amp; Investment Corporation saw a strong recovery in 
            its net profits in 2003, rising by 44 per cent to $544 million 
            (RO209.2 million) following a fall in 2002. It recorded the second 
            highest ROE amongst the GCC banks at 28.1 per cent. Its ROA (3.2 per 
            cent) was equal highest with that of Commercial Bank of Dubai. Al 
            Rajhi operates exclusively on Islamic principles, giving it a 
            stronger position in certain markets. <br>
            <br>
            Its operations are notable for their simplicity, and tend to 
            concentrate on international trade, with expansion into areas such 
            as leasing, project finance and instalment sales. With the largest 
            branch network in the GCC, the bank benefits from strong customer 
            loyalty. It gathers non interest-bearing deposits through its branch 
            network, and utilises the funds in Shariah-approved investments. The 
            bank earns its money through taking risks with the deposits 
            gathered, not through interest. <br>
            <br>
            <b>Kuwait </b><br>
            National Bank of Kuwait (NBK), the flagship bank in Kuwait, also had 
            a good year. Net profit was up 16 per cent to $412 million (RO158.5 
            million) in 2003. ROE was higher at 21 per cent. The bank continues 
            to dominate its home market although some of the other Kuwaiti 
            banks, such as Gulf Bank and Commercial Bank of Kuwait, have begun 
            to compete strongly in selected market niches. <br>
            <br>
            As the domestic market offers limited opportunities for growth, NBK 
            will continue with its strategy of expanding geographically, 
            particularly within the region, through acquisitions. Going forward, 
            asset management in the Middle East will be one of the main areas of 
            focus for NBK, as it readily admits the retail banking market will 
            soon become saturated. <br>
            <br>
            Overall, Kuwaiti banks had a good 2003, with strong performances at 
            Kuwait Finance House (profits were up 17 per cent), CBK (up 49 per 
            cent), Burgan Bank (up 70 per cent) and Bank of Kuwait and the 
            Middle East (up 81 per cent). <br>
            <br>
            <b>Oman </b><br>
            BankMuscat ranks as the largest banking entity in Oman, a position 
            it attained via a string of mergers and acquisitions, the latest 
            being that of NBO, which has enabled to have an assets base of 
            RO2.56 billion, enough to catapult it into the Top 20 GCC banks next 
            year. The bank itself came into being after a merger of Bank of 
            Muscat and Al Bank Al Ahli Al Omani. The wave of mergers and 
            acquisitions reflect favourably on the bank. BankMuscat has emerged 
            as the Sultanate's premier bank in term of assets, capital and 
            profit. In addition, the bank also raised its asset ranking against 
            stalwart GCC banks. <br>
            <br>
            However, NBO had reported weak results in 2003 - a net loss of $134 
            million (RO51.5 million). The large loss was due to $184 million 
            (RO70.8 million) in provisions for cleaning up the poor credit 
            portfolios in all three countries in which the bank operates, as 
            well as an extraordinary item of $23.7 million (RO9.1 million), 
            which represented a disputed loss of foreign exchange on placements 
            with the Central Bank of Egypt. The bank had, however, managed to 
            turn around the tide and posted profits of RO3.044 million in the 
            first half of 2004 before the merger announcement with BankMuscat 
            came in last month.<br>
            <br>
            The other listed Omani bank to have posted a below par performance 
            in 2003 was OIB, which saw its income go down 10.2 per cent to 
            RO44.21 and profits plunge 28.6 per cent to RO13.22 million. The 
            bank has posted profits of RO7.26 million in H1 2004. BankDhofar, 
            which acquired Majan International Bank in 2003 and has followed the 
            M&amp;A approach to growth like BankMuscat, posted a 14 per cent jump in 
            revenues at RO32.72 million in 2003 over RO28.71 million in 2002. 
            Its net profits also witnessed an impressive 22.44 per cent increase 
            - from RO8.30 million in 2002 to RO10.16 million in 2003. It has 
            posted net profits of RO5.63 million for H1 2004. <br>
            <br>
            <b>Qatar </b><br>
            Qatar National Bank (QNB), the seventh largest bank of the GCC based 
            on capital, is the dominant and major player in the Qatari banking 
            sector, with a high market share of almost 50 per cent. The bank 
            enjoys privileged access to government business due to its size and 
            50 per cent State ownership. <br>
            <br>
            QNB's performance in 2003 was sound with net profits up by 11 per 
            cent to $176 million (RO67.7 million), driven largely by 
            non-interest income. Net fees and commission income was up 44 per 
            cent due to increased retail lending business, investment banking 
            and equity business. Its ROA is a high 1.84 per cent. Both retail 
            and corporate banking business performed well in 2003. <br>
            <br>
            Going forward, the prospects for QNB largely depend on the state of 
            the domestic economy and the government's finances, and on this 
            note, the outlook remains very positive. High oil prices during the 
            past few years have boosted Qatar's fiscal and external account 
            positions. Oil and Liquefied Natural Gas (LNG) account for more than 
            40 per cent of GDP and 70 per cent of total exports. In the medium- 
            to long-term, the economy will benefit significantly from the 
            development of gas-related fields, as well as from the construction 
            of hydrocarbon-related downstream projects. <br>
            <br>
            Commercial Bank of Qatar (CBQ) also had a good 2003. Loans and 
            deposits rose strongly by 39.4 and 42.9 per cent, respectively. 
            Total assets were up by 43 per cent. Operating income, before 
            provisions and expenses, rose to a record level of $116 million 
            (RO44.6 million). Net profit rose by 60 per cent to $66.7 million 
            (RO25.7 million). Net profits were held back by the need to set 
            aside additional provisions of principal and interest for 
            non-performing or poor quality debts, but was still at an all-time 
            high. Interest income rose by 8 per cent to $94 million (RO36.2 
            million). Other income climbed 48 per cent to $45 million (RO17.3 
            million) on the back of a 57 per cent increase in forex earnings and 
            a 91 per cent increase on the bank's own investment earnings. During 
            the year, CBQ commenced a programme of investment to upgrade 
            customer-facing technologies and to expand its branch network. <br>
            <br>
            <b>The UAE </b><br>
            Fifteen UAE banks made it to the Top 50 GCC bank list in 2003. 
            Emirates Bank International's (EBI) net profits improved by 9 per 
            cent in 2003 to $174 million (RO66.9 million), and its ROA is good 
            at 2 per cent. EBI's total assets rose by 17 per cent in 2003 aided 
            by growth in its loan portfolio. The bank is majority-owned by the 
            government of Dubai and receives its strong support. Going forward, 
            EBI expects to increase its presence in the region by acquiring 
            small equity participations in financial entities. It has 
            diversified its business considerably by setting up subsidiaries to 
            handle specialised activities such as credit cards, insurance and 
            investment banking.<br>
            <br>
            MashreqBank improved its net profit rankings by posting $163.6 
            million (RO62.9 million) net profits last year - a 23 per cent rise 
            over 2002. MashreqBank CEO, Abdul Aziz Al-Ghurair said the Group 
            achieved improved performance across all business segments. The 
            bank's capital also rose and it became the first UAE bank to offer a 
            comprehensive mortgage product with the launch of home loan services 
            in 2003, a finance facility for both resident and non-residents. The 
            bank has redefined its corporate banking strategies and strengthened 
            its risk management processes, notes a Capital Intelligence report.
            <br>
            <br>
            Bahrain <br>
            For decades, Bahrain has prided itself on being the financial 
            gateway to the Gulf and, today, the Bahrain Monetary Agency (BMA, 
            the central bank) licenses 362 institutions, of which 186 are banks 
            and banking-related institutions, 163 are insurance and 
            insurance-related firms and 13 are capital market brokers. Bahrain 
            is viewed as being well ahead of its rivals in monetary expertise 
            and in terms of regulation, particularly with its efforts to unify 
            the disparate and interpretative regulations of Islamic banking. <br>
            <br>
            The most profitable bank in Bahrain in 2003 was Arab Banking 
            Corporation, which posted net profits of $119.7 million (RO46.04 
            million) compared with a $51 million (RO19.62 million) loss the 
            previous year. This was followed by its traditional rival, Gulf 
            International Bank, with profits of $106.1 million (RO40.81 
            million), Ahli United Bank, which posted a $87.1 million (RO33.5 
            million) profit in 2002, and Investcorp, with $75.13 million (RO28.9 
            million) in profits following a $8.4 million (RO3.23 million) loss 
            in 2002. <br>
            <br>
            Overall, profits at Bahrain's locally incorporated banks more than 
            doubled during 2003, compared with 2002. The BMA noted that the 29 
            Bahrain-incorporated commercial, investment and offshore banks, both 
            conventional and Islamic, posted aggregate net profits of $714.8 
            million (RO274.92 million) for 2003. This was an increase of 134 per 
            cent over 2002's profits of $305.6 million (RO117.54 million). 
            Commercial banks represented 24 per cent of 2003 profits while the 
            share of offshore banks and investment banks was 48.5 and 27.5 per 
            cent, respectively. <br>
            <br>
            Confidence in the GCC is currently high and most economies are 
            registering good rates of growth, supported by the current high oil 
            prices. Banks are benefiting from the operating environment, are 
            liquid and generally in good financial health. Returns for GCC banks 
            in H2 2004 should again be sound. First half 2004 results have in 
            general been robust, continuing the trend in 2003.</font></p>
