                      <td colspan="3" height="34" bgcolor="#4078A9">
						<b><font face="Verdana" size="1" color="#FFFCE8">IS 
                        THERE A NEED TO STRENGTHEN CORPORATE GOVERNANCE NORMS 
                        AROUND THE WORLD? </font></b></td>
  </tr>
  <tr>
                      <td height="96" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">The Corporate world is 
                        growing from strength to strength and large 
                        organisations are becoming a place for investors to 
                        trust their hard earned money. The various stakeholders 
                        like the investors, customers, suppliers, government and 
                        employees depend on the board to steer clear of any 
                        issues and ensure that their respective wealth is 
                        continuously enhanced. In such a situation it is 
                        imperative that governance norms are strengthened and 
                        they are above board. Every institution should build 
                        ethical, transparent, dynamic and responsive controls 
                        that spans across the organisation.<br>
&nbsp;</font></td>
    <td width="1" height="96" bgcolor="#BCBEC0"></td>
                      <td height="96" valign="top"> 

<p align="justify"><font face="Verdana" size="1">There are enough rules and regulations to protect investors
and companies in most countries across the globe, but when
human greed comes into play, then rules go out of the window.
Even when the audit function evolved, the first thing that was
done was bringing in a rule for bank confirmations – cash was
always physically counted. In the recent case involving Satyam
Computers, this basic function was ignored or the evidence given
to auditors was fake. So there are time tested laws and accounting
standards, the only thing that needs to be done is to be honest.</font></p>

                      </td>
  </tr>
  <tr>
                      <td colspan="3" height="25" bgcolor="#D64F30">
						<b><font face="Verdana" size="1" color="#FFFCE8">WHAT 
                        CAN BE DONE TO SEE THAT CORPORATE FRAUDS DO NOT TAKE 
                        PLACE?</font></b></td>
  </tr>
  <tr>
                      <td height="104" valign="top"> 

<p align="justify"><font face="Verdana" size="1">All organisations should have a stated 
governance policy which
is transparent and is implemented across the organisation. A
mechanism in the form of a committee should be in place to audit
the implementation of the norms at frequent intervals and bring
to notice any lapses which should be dealt with an iron hand. The
directors should be rotated and their continuous term should
be restricted. Independent directors should be appointed with
care and they should be people with impeccable calibre who can
spare time to review board resolutions and ensure that board
meetings are not a mere formality. Directors should continuously
apprise themselves of the developments across the globe in similar
industries and have access to the management team and report
any conflict of interest.<br>
&nbsp;</font></p>

						</td>
    <td width="1" height="104" bgcolor="#BCBEC0"></td>
                      <td height="104" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Everyone has to take on a moral responsibility 
as there are limits
to what policing can achieve. If external agencies and auditors do
their work honestly, such cases will not take place. Things like a
peer review can help. In such a system various accounting firms
look at the work of each other periodically, and it is a system
which can work as a deterrent. Everyone has to realise that if you
aspire for more than what you can deliver or if you are in a hurry
to make a fast buck, then it is just a matter of time before the law
catches up. Economic crimes need to be checked, because whether
it is a Bernard Madoff scam or a Lehman collapse, all of us end up
paying for these loses.</font></td>
  </tr>
  <tr>
                      <td colspan="3" height="32" bgcolor="#697C3A"><b> <font face="Verdana" size="1" color="#FFFCE8">
                      &nbsp;IS THERE A CASE FOR LOOKING INTO THE ROLE OF INDEPENDENT 
                      DIRECTORS AND AUDITORS TO IMPROVE THINGS?</font></b></td>
  </tr>
  <tr>
                      <td height="82" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Yes. There is a definite 
                        need for this as independent directors and auditors stay 
                        for a long period of time in any organisation and this 
                        leads to a distinct possibility of colluding for an 
                        ulterior<br>
                        motive. This needs to be reviewed in terms of continuity 
                        of people, compensation, family relationships, stock 
                        holding, rotation of responsibilities etc. I am sure 
                        that if right controls are in place and are reviewed 
                        constantly and acted upon transparently, the confidence 
                        of the various stakeholders will remain high in the 
                        corporate world.</font></td>
    <td width="1" height="82" bgcolor="#BCBEC0"></td>
                      <td height="82" valign="top"> 
						<p style="margin-left: 5; margin-right: 5" align="justify">
						<font face="Verdana" size="1">Independent directors are 
                        supposed to understand a business from an external point 
                        of view and judge its functioning as an outsider. As the 
                        chairman of the audit committee, a director can ask for 
                        the company accounts and ask the right questions to 
                        auditors. Most directors have the business skills as 
                        they are people who have been chosen on the strength of 
                        their antecedents, it’s just that they need to do their 
                        duties faithfully.<br>
&nbsp;</font></td>
