                <br>
                </font><b><font face="Verdana" size="4">A global citizen<br>
            </font></b><font face="Verdana" size="1"><b>The worst aspect of 
            being a diplomat is leaving friends behind, with every new posting, 
            says HE Anil Wadhwa, India’s Ambassador to Oman. From philanthropic 
            inclinations to sporting enthusiasm, HE Wadhwa talks personal, in an 
            interview with Sunil Fernandes<br>
            </b><br>
            <img border="0" src="person.jpg" align="left" hspace="8" vspace="6" width="250" height="343">As you enter the office of HE Anil 
            Wadhwa, India’s Ambassador to Oman, you can’t miss the huge 
            bookshelf. Thirty minutes into the conversation and you realise, HE 
            Wadhwa is well read. He can discuss every topic from cuisine to 
            sports to politics. A sporting enthusiast, he has played a host of 
            games, and loves doing so, even today. Over 29 years of service has 
            taken him to different continents witnessing different cultures, 
            cuisines and conditions. He has had postings in Poland, Lithuania, 
            Hong Kong, China, Switzerland, Holland etc. <br>
            <br>
            He has had a stint at the International Organisation for the 
            Prohibition of Chemical Weapons (IOPCW). “We had signed a treaty on 
            chemicals weapons and I was amongst the 20 chosen by the Secretary 
            General of IPOCW and was posted in Hague. We were the ones to put 
            all the procedures and recruitment in place for the organisation. I 
            used to be a spokesperson for the Executive Secretariat. I was later 
            involved in government relations and political affairs,” he says.
            <br>
            <br>
            <b>Experience in Muscat</b><br>
            HE Wadhwa has been in Muscat for the last 16 months and finds Muscat 
            a fascinating place. “I didn’t expect Muscat to be what it is. I 
            thought it would be a difficult place to stay and weather a little 
            harsh. I have had a pleasant surprise. The work here is very 
            interesting and the community is huge,” he says.<br>
            <br>
            With the frequent postings, it is obvious that one would carry 
            memories along. “Each place has had a special significance for me. 
            In China, I remember wandering the streets, with a sea of humanity. 
            It was very well organised, despite having a large population. In 
            Hong Kong, the lanes and the roadside food, enabled me take back 
            special memories. In Geneva it was the skiing and we used to go 
            skiing, as a family, mostly on weekends. In Oman of course, it has 
            to be the beaches, “ he says.<br>
            <br>
            <b>One family, two ambassadors </b><br>
            Interestingly, HE Wadhwa’s wife HE Deepa Gopalan is also an 
            ambassador. With both of them working as ambassadors in different 
            countries, a question would remain, on how often they meet. “Until 
            now, it was difficult, as she was posted as Ambassador in Sweden. 
            But with her Qatar posting, I am sure we could meet more often. 
            Also, with our earlier postings in Poland and Sweden, we would meet 
            more often. The government is kind enough to give us postings that 
            are geographically close,” he says.<br>
            <br>
            <img border="0" src="person1.jpg" align="right" hspace="8" vspace="6" width="300" height="267">Relocating can always have its disadvantages and HE Wadhwa has also 
            been a witness to some of it. “As you get older, it becomes a little 
            difficult; you don’t have the same energy levels. But over the 
            years, you get mentally prepared for it. As long as you are mentally 
            prepared, it is fine,” he says<br>
            <br>
            Of course, in the process one does loose friends and acquaintances. 
            “The worst part of relocating is leaving old friends and making new 
            ones. These days with the kind of technological advantage, it 
            becomes a lot easier. I have learnt lessons from the past and keep 
            in touch. Many friends from Poland and Lithuania have visited 
            Muscat,” he says. According to HE Wadhwa, the other area of concern 
            while relocating is that children tend to miss their friends. “As 
            youngsters, children find it more difficult to adapt, then adults,” 
            he says. <br>
            <br>
            <b>Sport enthusiast</b><br>
            Though, time would always be a constraint for HE Wadhwa, he does 
            manage to spend his leisure time engaging in sporting activities. In 
            fact, he has played a host of games, and is a sporting enthusiast. 
            “I started golfing when I was in Netherlands. I am playing on and 
            off. I am waiting for Muscat Hills to start. I do play bridge as 
            well. I have been playing tennis since I was seven years old, and 
            continue to do so, though not as often as I used to. In college, I 
            used to play hockey. But, you cannot pursue a game like hockey, once 
            you get older,” he says.<br>
            <br>
            In the last 29-years, HE Wadhwa’s postings have taken him to several 
            places, that have different cuisines. However, he particularly 
            savours Chinese delicacies. “I have lived in China and Hong Kong for 
            many years. I have relished different kinds of Chinese cuisine over 
            the last several years. In the Hague, I learnt how to cook. I can 
            cook some basic foods of all kind. And cooking, takes your mind off 
            and can act as a stress buster,” he says. <br>
            <br>
            <b>Philanthropic activities <br>
            </b>“I think I would like to join an organisation that does 
            something on the environment in India. Maybe, I would do it on my 
            own or assist an NGO,” he says. Civic sense is also an area of 
            concern for HE Wadhwa. “We can do a lot to instill a civic sense in 
            India. I feel I can do a lot in this area. Even if I can work for an 
            organisation to accomplish this, I do not mind,” he says.<br>
            <br>
            With a sizeable Indian community in Oman, HE Wadhwa has chalked out 
            some initiatives, he wants accomplished before he completes his 
            stint in Oman. “The most important thing I would like to see 
            accomplished in Muscat is to ensure that the Indian community works 
            together. The more united the community is, the greater are the 
            achievements. Also, the community should be united for those in 
            need, “ he says. HE Wadhwa has himself initiated a few things for 
            the betterment of the community. “ We have made a good beginning, 
            with the start of the Asha initiative, which works as the core 
            group. It started out as volunteers, with ladies who wanted to help 
            people in distress. I want to ensure when I leave, systems are in 
            place so that those in distress can get assistance,” he says. </font></p>

