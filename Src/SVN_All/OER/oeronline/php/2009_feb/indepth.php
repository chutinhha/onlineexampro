		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Gateway to new 
                opportunities</font></b><font face="Verdana" size="1"><br>
                <b>The Oman-US FTA implementation comes with a host of potential 
                economic benefits and challenges for the Omani businessmen. 
                Akshay Bhatnagar takes stock of the FTA and its asso-ciated 
                impact on the Sultanate’s economy<br>
                </b><br>
                Oman has welcomed 2009 with a positive development that could 
                alter the economic landscape of the Sultanate – the 
                implementation of Oman-US FTA (free trade agreement). For some 
                Omani businesses that have been on the brink of closure it could 
                bring a fresh lease of life; for many it could open up a host of 
                new business opportunities for growth and; for few it could mean 
                challenging times ahead</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
                <img border="0" src="depth.jpg" width="500" height="293"><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <br>
                <br>
                If we look at the current trade between the two countries, it 
                was in excess of $2bn in 2008 (Jan-Nov). The Omani exports to US 
                were valued at $819mn whereas imports were around at $1.2bn. 
                Oman mainly imports transport equipment, road vehicles, 
                machinery, scientific instruments, food and agriculture items 
                from US. On the exports front, the Sultanate ships petroleum, 
                apparel, natural gas, iron &amp; steel and manufactured items to US.<br>
                <br>
                <img border="0" src="depth1.jpg" align="left" hspace="8" vspace="6" width="183" height="287">Though 
                the US economy is currently reeling under recession but it is 
                still the world’s largest market ($14 trillion) that could open 
                a new gateway of growth opportunities for Oman (GDP $40bn). 
                Talking about the FTA, the US Ambassador to Oman HE Gary A. 
                Grappo said: “In today’s challenging global environment, the 
                US-Oman Free Trade Agreement will provide new opportunities and 
                expanded horizons for economic development to Omani and to 
                American businesses alike.”<br>
                <br>
                <b>Key FTA aspects<br>
                </b>After hectic parleys and intense rounds of discussion since 
                March 2005 when the first round of FTA negotiations took place 
                in Muscat, the agreement came into effect from January 1, 2009. 
                Overall from Oman’s side, 14 negotiating sub-groups dealt with 
                US on their respective subjects including market access for 
                goods, textiles and clothing, sanitary &amp; phytosanitary measures 
                (SPS), technical barriers to trade (TBT), government 
                procurement, investment, cross-border services, financial 
                services, telecommunication services and electronic commerce, 
                intellectual property rights, customs matters and rules of 
                origin, labour, environment and legal matters.<br>
                <br>
                The FTA aims at realising extensive market access liberalisation 
                both in terms of goods (both agricultural and industrial 
                products) and services. It eliminates duties on goods imported 
                by both the countries from the other. Oman has provided 
                immediate duty free access on virtually all products with the 
                exception of a few such as vegetables, whose duty are to be 
                eliminated in five equal annual stages, and others such as 
                dates, lemons, bananas, pork, alcohol and tobacco whose duty is 
                to be reduced to zero over a period of 10 years. However, the 
                Sultanate is free to impose sales tax or any other forms of 
                internal taxes. With the FTA in place, the demand for American 
                vehicles, heavy machinery, office equipments and IT &amp; 
                telecommunication products is expected to go up in Oman.<br>
                <br>
                <img border="0" src="depth2.jpg" align="right" width="182" height="287">The 
                US on the other hand has eliminated duties on all items, most of 
                them with immediate effect and few after a transitional period. 
                This is expected to give a fillip to Omani exports to the US and 
                would also encourage US and other countries’ companies to invest 
                in Oman in order to produce and export goods to the vast US 
                market, taking advantage of duty-free preferential access.<br>
                <br>
                “The FTA is a major step beyond generalized System of 
                Preferences (GSP) in that it offers unlimited access to the US 
                market with no limitations on imports. As Octal is concerned, 
                our strategy is to export at least $150mn a year to the US and 
                the FTA enables us to compete very favourably against other 
                exporters paying duties of 3-6.5 per cent. This is very 
                significant. Anticipation of the FTA, was a main reason for 
                setting Octal in Oman as opposed to other GCC countries,” said 
                Nicholas Barakat, managing director, Octal Petrochemicals that 
                is developing a PET and APET based global hub in Salalah at a 
                combined investment of more than $1bn. Octal will have an annual 
                export potential in excess of $1bn.<br>
                <br>
                The FTA is expected to revive the fortune of textiles and 
                apparel sector in Oman. The sector contributed $97mn (including 
                $69mn re-exports) to the export kitty in 2006 and the figure is 
                expected to go up substantially in the coming years as a result 
                of the FTA. “Textiles and apparel products will be rendered 
                duty-free access upon entry (in US) as long as they satisfy the 
                rules of origin requirement applicable to textiles and apparel, 
                such as containing Omani or US yarn and fabric. The US has 
                agreed to make one major concession. An annual total quantity of 
                50 million square meters in each of the 10 twelve-months (10 
                years) periods after the entry into force of the agreement, 
                shall enjoy duty-free access even if these did not meet the 
                rules of origin criteria,” says Dr Said Amer Al-Riyami, Economic 
                Expert, Ministry of Commerce &amp; Industry.<br>
                <br>
                Yousuff Ahmed, Executive Director, Oman Textile Holding Co is 
                bullish on the FTA as he said, “The apparel and clothing sector 
                is a significant beneficiary. The FTA will lead to revival of 
                the units in the Sultanate which are not doing well. For Oman 
                Textile Mills, the FTA offers a tremendous opportunity at our 
                doorstep. Oman has the advantage of location, progressive 
                policies of the government and if we could build volumes, Omani 
                textiles and apparels could carve a niche in US market.”<br>
                <br>
                The agreement has put in place specific rules of origin to 
                ensure that only Oman and US goods shall benefit from the 
                preferential duty treatment. “The Omani goods that shall benefit 
                from the agreement are goods originating from Oman and goods 
                that have undergone a substantial transformation in Oman, with 
                at least 35 per cent value added. This provision would encourage 
                genuine industries to be established in Oman, and would 
                discourage simple-assembly industries. The agreement also allows 
                regional cumulation formula by which Omani industries can 
                utilise inputs from other Middle East countries that have 
                entered into FTAs with both Oman and the US,” informed Dr Said.<br>
                <br>
                <img border="0" src="depth3.jpg" align="left" hspace="8" vspace="6" width="179" height="287">He 
                added, “Oman would allow access to US service suppliers, like 
                consultative professional services, on a more liberal scale than 
                it is presently allowing under its commitments as a member of 
                the WTO. That would attract greater US investment in services in 
                Oman, providing more employment opportunities for Omanis. It 
                would also mean more efficient and more competitive services 
                benefiting Omani consumers and Omani businesses.”<br>
                <br>
                Oman has put a negative list that contains some sensitive 
                services which are not open to US services and US service 
                suppliers. The list includes real estate brokerage services, 
                employment placement services, investigation and security 
                services, tourist guide services, internal waterway 
                transportation services, taxi transportation services, and 
                certain small businesses.<br>
                <br>
                On the investment front, Oman has created a level-playing-field 
                for US investors. Oman will give the same treatment to US 
                investors as it gives to GCC investors. This move is expected to 
                increase foreign direct investment (FDI) from US. As a 
                safeguard, Oman has put certain sensitive sectors in the 
                negative list where US investors may be barred from making 
                investments. These include printing and publishing services, 
                retail photographic services, radio and television transmission 
                services, news agency services and air transportation services. 
                On retailing services, like supermarkets, US investors may own 
                up to 70 per cent of the equity in any enterprise established in 
                Oman that is valued at less than $5mn. For greater certainty, US 
                investors may own up to 100 per cent of the equity of any 
                enterprise established in Oman that is valued at greater than 
                $5mn. However, after 31 December 2010, US nationals may own up 
                to 100 per cent of the equity in any enterprise established in 
                Oman that is valued at greater than $1mn and that supplies 
                retailing services. It could lead to increased competition for 
                regional and European retail heavyweights that are operating in 
                Oman from US retail giants.<br>
                <br>
                <b>Path ahead<br>
                <img border="0" src="depth4.jpg" align="right" hspace="8" vspace="6" width="179" height="289"></b>Though 
                concerns have been raised that the FTA may negatively impact 
                some of the local players that are yet to achieve a high level 
                of competitiveness but the government has quashed such 
                apprehensions. HE Maqbool Ali Sultan, minister of commerce and 
                industry while addressing a gathering of businessmen at Oman 
                Chamber of Commerce and Industry (OOCI) categorically stated: 
                “We do not believe that imported products from US are going to 
                create problems for Omani producers as a result of the 
                competition such products may raise. Such competition, in fact, 
                may benefit the Omani producers and companies in the long run by 
                inducing them to give greater attention to the productivity and 
                competitiveness elements.”<br>
                <br>
                The FTA provides the golden opportunity for Oman to optimise on 
                the exports potential in US. In order to be competitive in an 
                intensely aggressive market like US, apart from other things 
                Oman needs to build sector competitiveness. The best move 
                forward will be to identify key target sectors that have maximum 
                opportunities for trade.<br>
                <br>
                According to Booz Allen Hamilton, the sector specific 
                initiatives to improve the sector competitiveness can be private 
                sector driven but will also require government support. In the 
                current financial squeeze, the authorities need to ensure 
                increased investments and commercial lending, provide credit 
                guarantees and make policy changes to create and strengthen 
                certain sectors to benefit from the opportunities under the FTA. 
                The real benefits of the FTA may not be reflected in the 
                immediate period but the strides made by Oman to benefit from 
                the FTA will definitely help the Sultanate to raise its market 
                competitiveness to gain from other trade agreements also that it 
                is currently negotiating, unilaterally or as part of the 
                regional bloc. </font> 
