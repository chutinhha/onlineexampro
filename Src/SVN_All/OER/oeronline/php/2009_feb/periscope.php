            <td width="70%" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
            <font face="Verdana" size="4">WHAT NEXT FOR GLOBAL CURRENCIES?<br>
            </font>
            </b><font face="Verdana" size="1"><b>The US business cycle has not 
            yet bottomed, despite the horrific numbers in retail sales, 
            unemployment, auto sales, construction and industrial production. 
            This is the result of the pro-activeness shown by the US treasury 
            and the Federal Reserve in their monetary/fiscal stimulus and 
            banking guarantees<br>
            <br>
			<font color="#CC3333">By Matein Khalid</font></b><br>
            <br>
            <img border="0" src="peri.jpg" align="right" hspace="8" vspace="6" width="125" height="148">It 
            is ironic that the dollar actually strengthened against the Euro 
            after the US posted its worst job loss report since the end of World 
            War II. However, this is only a testimony to the extreme bearishness 
            with which investors view Europe, as even the German export colossus 
            slips into recession. Even the austere, monetarist Jean Claude 
            Trichet, President, European Central Bank can no longer justify ECP 
            (European Customs Practice) hawkishness now that Eurozone inflation 
            has begun to fall below 2 per cent at a time when one in seven 
            German industrial jobs are related to auto manufacturing.<br>
            <br>
            The other explanation for dollar strength in early 2009 (the Euro is 
            down from 1.47 in December to 1.34 in mid January) is clearly the 
            brutal sell off in commodities markets as recession spreads across 
            the planet. Oil was unable to hold onto $40 a barrel even though 
            Saudi Arabia led the steepest production cuts in the history of OPEC 
            in its Algeria meeting and a cold winter in Europe. A third pillar 
            of dollar strength is the imminent inauguration of President Obama 
            and the expected $775bn stimulus package to revive the US economy.
            <br>
            <br>
            <b>Fly in the ointment</b><br>
            <img border="0" src="peri1.jpg" align="left" hspace="8" vspace="6" width="176" height="419">I believe the dollar strength is temporary as the Bernanke Fed has 
            adopted quantitative easing or unorthodox monetary policies to 
            combat deflation risk. The overnight dollar borrowing rate is now 
            near zero and three month dollar LIBOR has plunged from 4.80 to an 
            incredibly low 1.16 per cent now. This plunge in dollar interest 
            rates is matched by a fall in credit risk spreads on AAA corporate 
            bonds and commercial paper. Note also that the Chicago Volatility 
            Index (VIX), the quintessential Wall Street index of greed and fear 
            in the stock market, has plunged from 80 in October to 42 now. This 
            is significant because the scramble for dollars began last year as 
            risk spreads spiked higher and global funds forced unprecedented 
            redemption flows that triggered a surge of “repatriation” dollar 
            buying. However, as risk spreads and repatriation flows ease, the 
            dollar does not act as the safe haven currency for the world. <br>
            <br>
            The US business cycle has not yet bottomed, despite the horrific 
            numbers in retail sales, unemployment, auto sales, construction and 
            industrial production. However, the US Treasury and the Bernanke Fed 
            have been proactive and aggressive in their monetary/fiscal stimulus 
            and banking guarantees. This is the fundamental difference between 
            the US and Japan in the 1990s, whose central bank also ran the 
            printing presses to save zombie banks bankrupted by the collapse of 
            the Nikkei/Japanese real estate market. So it is unwise to bet that 
            the dollar will appreciate now, just as the yen appreciated in the 
            1990s on quant easing.<br>
            <br>
            It is doubtful if even Obama’s stimulus triggers robust GDP growth 
            in 2009 even as the Fed Funds rate will remain close to zero. There 
            is anecdotal evidence that central banks in the Arabian Gulf, 
            Southeast Asia, China, South Korea and Japan will probably raise, 
            not cut, their proportion of Euro reserves now that it is far below 
            its 1.60 peak last summer. It is realistic to expect Euro/dollar to 
            rise as high as 1.45 in the next three months.<br>
            <br>
            <b>Yen takes off </b> <br>
            The Japanese yen has remained strong against the dollar at 89/90 
            since New Year because Japanese exporters have hedged aggressively 
            to protect their margins. This hedge is cheap since there are now 
            zero interest rate differentials between Japan and the US. With 
            Toyota Motor posting its first loss since the 1970s, it is all too 
            probable that the Bank of Japan will intervene to prevent a surge in 
            the yen to record postwar levels, to the early 80s in dollar-yen. 
            Finance Minister Nakagawa has made it clear that dollar-yen below 90 
            could well trigger intervention. It is also likely that the Taro 
            Aso’s Cabinet, with extraordinarily strong disapproval ratings, will 
            not survive the next six months. I expect the Japanese yen to 
            depreciate gradually to 96-100 against the dollar in the next three 
            months. <br>
            <br>
            The Sterling’s collapse was one of the most significant themes of 
            2008 as the UK economy fell into a boom-bust black hole reminiscent 
            of the 1970s, the banking panic and nationalisation of High Street 
            banks RBS (Royal Bank of Scotland) and HBOS and the City of London’s 
            woes as a global financial centre. The Bank of England was forced to 
            capitulate and slash the base rate to its lowest since 1694. Yet it 
            is increasingly obvious that cable has priced in the bad news. 
            Euro-sterling has fallen from 0.98 to 0.89 now because the financial 
            markets believe that HM Treasury and the Bank of England have 
            responded aggressively to the UK’s financial distress. With even the 
            slightest evidence that UK home prices may stabilise and if New 
            Labour’s fiscal stimulus works, then cable could well rise to 1.70 
            and Euro-sterling could fall back to 0.75, not head to parity as 
            conventional wisdom dictates. </font></p>
