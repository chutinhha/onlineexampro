            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">
            <b>Betting on the rupee</b><br>
            The Satyam scandal, is the latest financial shock to the prospect of 
            new FII flows into the Sensex. Yet the $100 fall in crude oil 
            prices, India’s relatively low exposure to global trade, aggressive 
            easing in the CRR and repo rate by the Reserve Bank, resilient 
            remittances and a falling current account deficit all suggest that 
            the Indian Rupee will not deteriorate much beyond 50-51. While it is 
            unrealistic to expect 7 per cent GDP growth in 2009, India does not 
            face recession unlike the US, Europe or Japan. This suggests that 
            the buy-sell range for the Indian rupee could well be 50-46 in the 
            next twelve months. </font></p>
