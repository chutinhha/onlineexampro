                <td height="18"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4">You are in good 
                company</font></b><font face="Verdana" size="1"><br>
                <b>Embraer, the third largest aircraft company in the world is 
                working on increasing its presence in the GCC market. Mayank 
                Singh reports from its Sao Jose dos Campos headquarters in 
                Brazil<br>
                </b><br>
                <img border="0" src="aviation.jpg" align="left" hspace="8" vspace="6" width="300" height="165">It 
                has its own aircrafts, airports, pilots and over 23,000 workers, 
                it was Brazil’s largest exporter from 1999 to 2001 and the 
                second largest in 2002, 2003 and 2004 – with such an enviable 
                track record it is little wonder that the name Embraer evokes 
                instant respect and recognition in Brazil. Embraer, short for 
                Empresa Brasileira de Aeronáutica, is a Brazilian aerospace 
                conglomerate. The company produces commercial, military, and 
                corporate aircraft, and provides related aerospace services. It 
                is one of the three main exporters in Brazil. Among all aircraft 
                manufacturers, it currently has the third largest yearly 
                delivery of commercial aircraft and the third largest workforce 
                after Boeing and Airbus.<br>
                <br>
                <img border="0" src="aviation1.jpg" align="right" hspace="8" vspace="6" width="175" height="138">The company’s headquarters, main production facilities, and 
                engineering/design offices are in São José dos Campos, São 
                Paulo. Embraer also has a production plant and flight testing 
                facility in Gavião Peixoto, São Paulo. This facility includes a 
                16,400-foot (5,000 m) runway, the third-longest in the world. 
                Embraer has maintenance and commercial sites in the USA and 
                commercial offices in France, Singapore and China. As of July 8, 
                2008, Embraer had a firm order backlog totalling $20.9bn. 
                Indústria Aeronáutica Neiva is a subsidiary of Embraer which 
                produces airplanes and aircraft components. Its main product is 
                the Embraer EMB 202 Ipanema, the most employed agricultural 
                aircraft in Brazil and the first alcohol powered airplane. Neiva 
                delivered more than 3,700 aircraft until early 2006.<br>
                <br>
                The company set a new aircraft delivery record in 2008 by 
                delivering 204 planes in 2008, 20 per cent more than in 2007. 
                During Qtr4, 2008, Embraer delivered 44 E-Jets to commercial 
                aviation, eight Legacy 600s, two Phenom 100s and one Embraer 175 
                to Executive Aviation and four jets to the defence and 
                government segment. The company also increased the number of 
                customers with firm orders for jets from the Embraer 170/190 
                family, with the sale of 11 E-Jets to British Airways. The 
                company also delivered the first Embraer 190s to Holland’s KLM 
                Cityhopper and Embraer 195s to Azil Linhas Aereas, the first 
                Brazilian airline to operate an E-Jet. <br>
                <br>
                <img border="0" src="aviation2.jpg" align="left" hspace="8" vspace="6" width="178" height="232">The new sales of the Phenom executive jets family increased the 
                number of firm contracts to over 850 aircrafts. <br>
                <br>
                The company is looking at Oman as a potential market for its 
                commercial and defence aircrafts. Says Ricardo Bester, vice 
                president, marketing and sales, defence and government market, 
                Europe, Middle East and Africa, “We are in negotiations with the 
                Omani government for some of our aircrafts and we have got a 
                very positive response from them.” The company has supplied 
                aircrafts to a few countries in the region. <br>
                <br>
                ECC Leasing Company a 100 per cent subsidiary of Embraer, is the 
                world’s third largest civil aircraft manufacturer. Established 
                in 2002, it is responsible for the trading of Embraer’s 
                pre-owned aircraft portfolio. “We are uniquely positioned to be 
                able to offer our customers a one-stop shop that combines 
                leasing some of the world’s most successful regional aircraft 
                with the clear benefits of dealing with the aircraft 
                manufacturer. When you acquire an aircraft from us, you know it 
                comes with the manufacturer’s seal of approval,” says Bester.<br>
                <br>
                <b>Segment focus </b><br>
                Embraer has become one of the largest aircraft manufacturers in 
                the world by focusing on specific market segments with high 
                growth potential in commercial, defence, and executive aviation. 
                Say Fernando Ikedo, vice president, market intelligence, defence 
                and government market, Embraer, “We develop and adapt successful 
                aircraft platforms and judiciously introduce new technology 
                whenever it creates value by lowering acquisition price, 
                reducing direct operating costs, or delivering higher 
                reliability, comfort, and safety.”<br>
                <br>
                As a result, its aircraft’s provide excellent performance with 
                reliability, while being economical to acquire and 
                cost-effective to operate and maintain. Equally important, is 
                the fact that it provides a good product package, with 
                comprehensive aircraft and after-sales support for parts, 
                services, and technical assistance.<br>
                <br>
                Embraer employs more than 23,ooo people, 87.7 per cent of which 
                are based in Brazil. Based in São José dos Campos, Brazil, 
                Embraer was founded in 1969 as a government initiative and then 
                privatised on December 7, 1994. On March 31, 2006 the majority 
                of Embraer shareholders, including common, preferred and ADR 
                holders, approved Embraer’s capital restructuring proposal, 
                which consists of a simplified capital structure composed of one 
                type of shares (common shares) and will contribute to enhanced 
                corporate governance practices and transparency standards.<br>
                <br>
                <b>Going green</b><br>
                Embraer regards environmental quality as an important element of 
                its vision and aims at creating awareness and guiding all its 
                employees, as well as research work in this direction.<br>
                <br>
                The company’s web site states, ‘Environmental issues are 
                increasingly taking centrestage, as the consequences of the 
                damaging actions to the planet prove more obvious, which are 
                resulting in a drop in the quality of life and in the emergence 
                of problems such as the greenhouse effect, polar ice-cap 
                melting, water shortages, droughts etc. Technological advance 
                cannot be interrupted; neither can attention be deviated from 
                environmental issues. In other words, in technological 
                advancement resides the solution for the maintenance of 
                sustainable growth in order to ensure suitable conditions for 
                future generations. This is the reason for the growing concern 
                of Embraer with the environment.’<br>
                <br>
                In 2002, the Faria Lima Unit obtained its Environmental 
                Management System certification, by attaining the ISO 14001 
                Standard, and in 2004 this unit managed to maintain the 
                certification through Maintenance Auditing, carried out by the 
                ABS certifier (American Bureau of Shipping).<br>
                <br>
                To control all these certificates, Embraer created the 
                Environmental, Occupational Health, Safety and Quality 
                Integrated Management System (SIG-MASSQ), which aims to 
                facilitate the analysis of all actions in a global fashion, 
                integrating the company, employees, third parties, partners, the 
                environment, health, safety and quality, while always focusing 
                on the results of the company.<br>
                <br>
                The SIG-MASSQ results from Embraer’s awareness of the influence 
                it brings to bear on the lives of the people who participate in 
                its trajectory in the environment and in the regions where it is 
                installed. The implementation of the SIG-MASSQ is serving to 
                unite and apply the efforts of all towards an increasingly 
                competitive company, aware of its role in society. Embraer 
                growth is based on the fact that it takes its customers 
                satisfaction as the backbone of its corporate strategy. </font></p></td>
