			<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <br>
                </font><b>
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">As good 
            as it gets<br>
            </font></b>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><b>HE 
            Ahmed bin Abdulnabi Macki, the minister of national economy has sent 
            out a strong signal about the government’s commitment to 
            infrastructure and social spending in budget 2009. Visvas Paul D 
            Karra reports</b><br>
            <br>
            <img border="0" src="cover1.jpg" align="left" hspace="8" vspace="6" width="308" height="343">The 
            announcement of the State’s budget is usually the most awaited event 
            for the country as a whole because it reflects the economic health 
            of the nation. Since the budget is a plan that outlines the 
            government’s financial and operational goals, the budget may be 
            thought of as an action plan to allocate resources, evaluate 
            performance, and formulate plans in order to tide over 
            contingencies.<br>
            <br>
            The 2009 Budget of Oman was eagerly awaited by one and sundry, 
            virtually with bated breath, as it was coming against the backdrop 
            of a toxic financial crisis which had gripped the globe and had many 
            economic fatalities in its wake both in magnitude and numbers. While 
            prudent fiscal policies have helped the Sultanate to shore itself up 
            against the domino effect of the US subprime collapse in August 
            2007, the country was a mute witness to the subsequent sharp decline 
            in oil prices in late 2008, falling up to 70 per cent of the peak 
            prices in June-July 2008, attributed to weak global demand. Says 
            Nick Smith, CEO, The Wave, “The world is going through unprecedented 
            challenges and there has been a fundamental change in the structure 
            of things like how money gets lent, liquidity etc.” <br>
            <br>
            <b>The million dollar query</b><br>
            In this light, the uppermost question on everyone’s mind was whether 
            the government would curtail its public spending in 2009. Most 
            analysts felt that (See ‘Well Oiled’ article in OER’s January, 2009 
            issue) the economic outlook for Oman is cheerful. Experts had backed 
            this claim saying that the government will continue to invest in 
            development projects and create a positive climate for the private 
            sector to continue their planned investments.</font></p>
			<p align="center" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover2.jpg" width="458" height="474"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">This is exactly what the government did by taking a conscious 
            decision to increase spending as compared with the budgeted total 
            for 2008. The total expenditure for 2009 has been estimated at 
            RO6.4bn as compared to RO5.8bn in 2008, showing a 11 per cent 
            increase of RO624mn. This has sent out a loud and positive message 
            to the citizens that it is business as usual as far as the Sultanate 
            of Oman is concerned.<br>
            <br>
            Says AbdulRazak Ali Issa, Chief Executive, BankMuscat, “The 11 per 
            cent increase in expenditure, as compared to the 2007 figures, 
            reflects the government’s determination to ensure sustained economic 
            growth. Despite the projected deficit, the budget exudes optimism, 
            affirming the government’s commitment to follow through with all the 
            ongoing projects. The government has adopted a prudent approach to 
            sustain the Omani economy against the backdrop of the slump in oil 
            prices and crippling global financial crisis. Therefore the budget 
            is aimed at stimulating macroeconomic growth, suggesting that the 
            government is prepared to face any major economic downturn.”<br>
            <br>
            <img border="0" src="cover3.jpg" align="left" hspace="8" vspace="6" width="244" height="283">The budget projects a deficit of RO810mn, based on revenues of 
            RO5.6bn and spending of RO6.4bn. Education (RO791mn); health 
            (RO271mn); defence (RO1.5bn) and social security (RO225mn) remain 
            the major focus areas of government expenditure. The spending on oil 
            and gas has been pegged at RO1.3bn. The government hinted that this 
            budget could be a flexible one in that it may entail adjustments if 
            oil prices fall below the budgeted price. Says AbdulAziz Al Balushi, 
            CEO, Ahli Bank, “What is important is that what kind of government 
            expenditure will take place, if it is medium to long term then the 
            effects will not be seen this year.” <br>
            <br>
            Some of the other highlights of this budget include oil revenue 
            estimated at $45 a barrel with 805,000 bpd production; enhanced 
            spending on seventh five year plan; RO665mn allocated for new 
            development projects; and current, capital revenues estimated to 
            reach RO1.4bn. HE Ahmed bin Abdulnabi Macki, minister of national 
            economy and deputy chairman of the Financial Affairs and Energy 
            Resources Council, while presenting the 2009 Budget echoing the 
            positive sentiment said, “The anticipated drop in oil prices and 
            proceeds from non-oil exports will affect the budget negatively. But 
            with the expected increase in oil production (from 753,000 bpd to 
            805,000 bpd) along with the government’s commitment to proceed in 
            execution of planned projects and maintaining the current 
            expenditure levels on social services will have positive effect on 
            national economy.”<br>
            <br>
            <b>Strong budget<br>
            <img border="0" src="cover4.jpg" align="right" hspace="8" vspace="6" width="177" height="251"></b>Abdullah Salem Al Salmi, executive vice president, Capital 
            Market Authority, comments that the budget has come out strong and 
            optimistic. “This is a good sign and we all had been hoping for 
            this. HE Macki has also said in his speech that the national economy 
            is fine as is reflected in the fact that he has announced a deficit 
            budget by increasing the budgeted expenditure as compared to the 
            previous year,” he says.<br>
            <br>
            Ashok Hariharan, partner and head of tax, KPMG, Oman and UAE opines 
            that it is a responsible budget given the extremely difficult and 
            challenging environment currently faced not only by Oman but the 
            rest of the world. The government has taken the right steps in not 
            curtailing its expenditure even though revenue is budgeted to 
            increase by only 4 per cent. <br>
            <br>
            A detailed review of the budgeted revenue indicates that oil revenue 
            in fact has been budgeted by the government to decline by 3 per 
            cent, while gas revenues will increase by 8 per cent resulting in an 
            overall decline in total oil and gas revenue by 1 per cent compared 
            to the 2008 budget. “It is in this background that the government 
            needs to be complimented for continuing with its current development 
            programmes and in fact allocating a higher budget of RO800mn (10 per 
            cent 
            <img border="0" src="cover5.jpg" align="left" hspace="8" vspace="6" width="244" height="250">increase) towards development expenditure compared to the 
            previous year. It is measures like these which would help generate 
            confidence in the private sector to make sure that Oman is not 
            significantly affected by the global economic crisis,” says Hariharan. <br>
            <br>
            Indeed, an amount of RO665mn has been allocated for new development 
            projects. Further, the government’s participation in domestic, 
            regional and international institutions have been budgeted to 
            increase by 43 per cent to RO355m. This includes financing of 
            projects undertaken by Oman Oil Company, Oman Tourism Development 
            and other government financed institutions. The budgeted deficit has 
            increased to 5 per cent of the GDP or 14.4 per cent of total 
            revenues compared to 3 per cent of GDP and 7.4 per cent of total 
            revenue in 2008. The deficit as a percentage of GDP, whilst 
            certainly higher than 2008, cannot be regarded as alarming. This is 
            particularly appropriate given that inflation is expected to come 
            down to below 10 per cent during 2009 compared to the peak levels 
            reached in 2008 (12.4 per cent for January to October 2008).<br>
            <br>
            <img border="0" src="cover6.jpg" align="right" hspace="8" vspace="6" width="244" height="267"><b>Still attractive</b><br>
            HE Macki mentioned that Oman’s budget is targeting to achieve a one 
            per cent real growth in GDP compared to 10 per cent estimated to 
            have been achieved in 2008. This may appear to be very low, but 
            should be regarded as excellent given that the growth at current 
            prices would be negative for 2009 (-28 per cent) on account of the 
            oil prices, which is expected to be significantly lower than in 
            2008. Hopefully, the growth can be much higher than one per cent if 
            oil prices improve to $70 per barrel, which many acknowledge would 
            be a fair price for the future. HE Macki had indicated that he would 
            be comfortable with a price of $60 per barrel of oil. <br>
            <br>
            <b>Flip side of the coin</b><br>
            The government has addressed the industry concerns by allocating 
            higher project expenditure and envisaging real GDP growth in 2009. 
            This delivers a strong message that the country remains attractive 
            for investment. But all eyes remain on the price of oil. If it stays 
            below $45, then the government will do a rethink and may temporarily 
            shift its priorities by postponing some of the planned projects into 
            the future. The development of Duqm port (RO697mn) and other ports 
            (total allocation: RO922mn) will be pushed into the future. <br>
            <br>
            <img border="0" src="cover7.jpg" align="left" hspace="8" vspace="6" width="244" height="267">The price of oil is going to be a key determinant of the future, 
            points our Hans Erlings, CEO, Galfar Engineering and Contracting, 
            which has some of the largest and most prestigious government 
            projects in hand at the moment. “If the production of oil increases 
            and the price of oil stays above the budgeted price, then it is good 
            news. At the moment though, Galfar has the same workload even for 
            2009 because of our earlier contracts. The 2009 budget allocations 
            for infrastructure will only begin taking effect next year as 
            bidding for government contracts takes time. The present 
            infrastructure spending may turn into a source of income for 
            companies only after 2010.” <br>
            <br>
            He also strikes a note of caution, “Let us not expect too much from 
            the government at the moment. Since the oil price is highly unstable 
            there may not be sizeable increase in spending’s on new projects. 
            The government has a real focus on where it is going to spend and 
            therefore the priorities are roads, water, power etc. Alternatively, 
            the government may take recourse to the build-own-operate-transfer 
            (BOOT) model like the Sur-Quriyat road to continue infrastructure 
            development,” he says.<br>
            <br>
            <b>Elixir of life</b><br>
            <img border="0" src="cover8.jpg" align="right" hspace="8" vspace="6" width="244" height="267">The global financial crisis, which has been a kind of a 
            giant-killer, taking down some of the biggest names in the financial 
            world, saw the automobile industry bearing the brunt of the 
            after-effects of the slowdown. And Oman, which has a vibrant 
            auto-sector with both imports and re-exports, was not exactly 
            insulated from the harmful scythe which saw General Motors, the 
            biggest name in the automobile industry, needing a government 
            bailout. The automobile industry is directly linked to the financial 
            institutions with almost 80 per cent of cars purchases in Oman being 
            done with external funding either through banks or private financial 
            institutions. And therefore, the moment the global crisis reached 
            the shores of Oman, the banks froze their lending to maintain their 
            cash flows and liquidity levels. This put pressure on all the car 
            dealers as they were not able to clear their stocks which they had 
            already imported. The pressure was more on the automobile 
            re-exporters. <br>
            <br>
            “The Budget communicates a message of confidence and reassurance and 
            clearly demonstrates that there is still a positive economic climate 
            from the Sultanate of Oman,” says Paul Turner, National Sales 
            Manager, Oman Marketing &amp; Services (OMASCO).<br>
            <br>
            The increase in infrastructure expenditure shows that the government 
            is engaged in promoting its economic growth stimulus plan, 
            confirming the continuation of projects that everyone has been 
            waiting for. As government infrastructure spending continues, 
            private sector companies will continue with their planned 
            investments and this should instill the confidence necessary to 
            enable them to access the funds to ensure that plans proceed and 
            stay on schedule.<br>
            <br>
            <img border="0" src="cover9.jpg" align="left" hspace="8" vspace="6" width="174" height="267">In the year ahead, like other countries, Oman’s economy will be 
            impacted by the global financial crisis, but its strong fiscal 
            position reinforced during the oil boom will help the nation in 
            consolidating resources and reducing domestic debt, thereby limiting 
            the impact of the global crisis, says Issa. Further, the 2009 
            financial market environment appears to be challenging for business 
            with tight liquidity conditions, weak equity market and tightened 
            lending norms by banks. “BankMuscat, however, has built a strong 
            reputation and is confident of retaining its leading position in the 
            country,” Issa says.<br>
            <br>
            In Turner’s view point, “All of the original government projects are 
            still in place, translating into opportunities for the private 
            sector. If the government fast-tracks its budget spending, we will 
            quickly see the benefits emerging, resulting in more investments, 
            both foreign and direct, improved business cash flows and more 
            employment. I expect that this will stimulate the banks and other 
            financial institutions to ease their lending restrictions for 
            corporate entities as well as private individuals.”<br>
            <br>
            “The private sector is no doubt encouraged by the government 
            spending projected for 2009 and should fare well as long as they 
            tread cautiously by adopting an innovative business plan to sustain 
            growth amid the prevailing global financial crisis without running 
            into unmanageable risks,” opines Issa while saying that despite the 
            liquidity crunch, the stimulus in the budget should encourage the 
            private sector to target the impossible in 2009. Moreover, the 
            Central Bank of Oman’s (CBO) decision to reduce bank reserve 
            requirements and raise the loan-to-deposit ratio is a significant 
            attempt to improve liquidity.<br>
            <br>
            Says Erlings, “The private sector may switch into the “go-slow” mode 
            to make sure that they do not run into a cash flow crunch as they do 
            not know what the future holds for them. But at Galfar, we are 
            actually hiring more people both at the managerial and workers level 
            because we have so many ongoing projects which will run throughout 
            2009. And personally, I would like to work on the newly announced 
            port and airport projects.”<br>
            <br>
            <b>Look northward</b><br>
            <img border="0" src="cover10.jpg" align="right" hspace="8" vspace="6" width="244" height="238">Another important feature of the budget is the budgeted increase in 
            current and capital revenues of the government by 22 per cent. The 
            government is budgeting a 35 per cent increase in revenue from 
            income taxes for 2009 to RO225mn (despite there not being any 
            increase in the tax rates). Whilst this might appear strange, it is 
            understood that collections from income taxes during 2008 far 
            exceeded the budgeted amount of RO166mn. The government is also 
            expecting a 50 per cent increase in revenue from customs duties and 
            a 24 per cent increase from non-Omani labour licences. <br>
            <br>
            The message from this measure is difficult to miss, “All this 
            suggests that the government is not expecting any significant down 
            turn in 2009 in the economic activities. This is not unreasonable 
            considering the fact that many projects are at different stages of 
            implementation and it would be in everyone’s interest to make sure 
            that these are completed as planned,” says Hariharan.<br>
            <br>
            Government has allocated RO355mn for projects being executed by Oman 
            Waste Water Company; Salalah Sanitary Drainage Services Company; 
            Oman Oil Company; Oman Tourism Development Company; and Oman Dry 
            Dock Company. This coupled with the decline in inflation means that 
            government is actually expecting more economic activity, suggesting 
            a northward outlook for Oman while other economies are going south.<br>
            <br>
            The Oman Tourism Development Company (Omran) is the Government of 
            Oman’s tourism development and investment arm. It was established in 
            2005 with a mandate to develop the infrastructure and facilities 
            required to expand the country’s existing tourism industry. This 
            includes large-scale tourism projects which take advantage of the 
            company’s development expertise. Omran is currently involved in 
            developing more than 10 resort-style projects with a project value 
            in excess of USD $10 billion. Specific projects include new resort 
            villas, conference facilities, leisure resorts, golf courses and 
            hotels as well as the refurbishment of some of the major existing 
            international hotels. One of these projects is these project is the 
            Asian Beach Games Olympic village which currently being constructed 
            in Musannah. </font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover11.jpg" width="500" height="272"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">While underlining the positive tone of Budget 2009, Eng. Wael Bin 
            Ahmed Al Lawati, CEO of Omran, says that the budget is a clarion 
            call for people to remain calm as the government’s prudent fiscal 
            policies have helped the economy during this financial crunch. “We 
            have never overstretched ourselves by getting into debt problems. 
            The budget has managed to override the negative sentiment by keeping 
            up the development momentum of the past few years. This will have a 
            multiplier effect on all the sectors of the economy. We should keep 
            in mind that this is not a case of denial of the current situation. 
            A lot of my friends and me are really glad to be in a country that 
            is well managed under the wise leadership of His Majesty Sultan 
            Qaboos bin Said,” Lawati says.<br>
            <br>
            “The materialism that was created in the past two to three years was 
            phenomenal but from a moral and personal point of view it was not at 
            all healthy. Since it is an acknowledged fact that all economies go 
            through a cycle, the present situation was bound to happen. But as 
            far as we are concerned, we have not been asked to slow down on any 
            our projects and all our budgets have been approved,” says Lawati.<br>
            <br>
            In fact is it is the optimum time for construction activities as 
            commodity prices have gone down with a weakened global demand. 
            Certainly, large real estate projects will have to relook at their 
            business plans and reassess the costs with a rejigging of some 
            numbers and project costs. Lawati revealed that the Asian Beach 
            Games Village is on track and mobilisation has been done for 
            construction of the hotel and apartments.<br>
            <br>
            <b>Human resources development</b><br>
            <img border="0" src="cover12.jpg" align="left" hspace="8" vspace="6" width="197" height="238">Allocation for the educational and health sectors amounted to 49 per 
            cent of the total current expenditures indicating that the 
            government continues to attach top priority for developing its human 
            capital, the primary driver of any economy. Says Smith, “The 
            spending on health, education and social security has been raised 
            which shows that the government is ready to undertake its social 
            responsibility and this is a very positive statement.” The 
            educational sector accounts for 36 per cent at RO791mn while the 
            health sector accounts for 12 per cent or RO271mn of the total civil 
            ministries’ expenditure, showing an increase of RO43mn (19 per cent) 
            as compared to the previous year. <br>
            <br>
            “With 50 per cent of Oman’s population under the age of 25, it is a 
            sensible move to continue investing in human resources as health and 
            education are important components of the economic wheel of the 
            country. This is a commitment from the government that it will 
            provide the best health care and education to its people, come what 
            may,” says Salmi, while talking about budgetary allocation for 
            health and education.<br>
            <br>
            Apart from health and education, commitments for social security are 
            also substantial with RO339mn being set aside for the housing sector 
            to provide housing for limited income families and those families 
            displaced by the cyclone, points out Salmi.<br>
            <br>
            “One of the key elements of the budget has been the allocations for 
            social security and people improvement. It is quite reassuring and 
            heartening to see that the government has increased its expenditure 
            on health and education. This reiterates the commitment of the 
            government towards its people as a lot of countries don’t spend as 
            much as is needed on human development,” Lawati noted.<br>
            <br>
            The government has walked the talk by committing resources upfront 
            for the development of human resources, infrastructure and social 
            security. Now it is up to the private sector to respond in equal 
            measure. </font></p>
