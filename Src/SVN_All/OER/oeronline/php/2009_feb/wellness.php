                Iyengar yoga therapist<br>
                </b><br>
                The word Yoga comes from Yuj, which means yoking of all the 
                powers of the body mind and soul to reach peace and harmony. It 
                meas disciplining of the intellect, mind, emotions and the will 
                which enables one to look at life in all its aspects evenly. It 
                entails the concentration of the body, mind, will, intellect and 
                breath leading to inner peace. Peace dwells within us but since 
                we equate it with gratification of the senses people tend to get 
                frustrated. According to Patanjali’s Yogasutras there are eight 
                limbs of yoga. Says Yogacharya B K S Iyengar, “The restlessness 
                of the mind, intellect and self is stilled through the practice 
                of yoga and the aspirant reaches the peace within.”<br>
                <br>
                To achieve self-enlightenment you need a teacher or a guru. The 
                Iyengar school of yoga is different from other schools in three 
                ways – First, the technicality and precision of the poses (asanas) 
                are more intricate. Second, the correct sequencing of poses 
                triggers certain beneficial effects. Third, it uses a number of 
                props which helps people to maintain the poses with ease. <br>
                <br>
                In between the input and output of an asana there is a gestation 
                period (the time for which a pose is held). This period varies 
                from person to person based on the strength of their muscles, 
                joints etc. As a beginner one can start with 2-3 classes of 
                40-50 minute each, every week, but gradually one should try and 
                do the regime everyday. Today’s work environment creates a lot 
                of stress due to the pressure to perform. This is especially 
                true in the present economic scenario. Sitting for long hours on 
                the PC causes stiffness in the neck and backpain. Yoga is the 
                best way to destress the mind as well as to attain physical 
                fitness. CEO’s and senior executives practising yoga have 
                experienced and shared that yoga helps them attain clarity of 
                mind to take better decisions Here are a few asanas that I would 
                recommend for working executives.</font></p>
