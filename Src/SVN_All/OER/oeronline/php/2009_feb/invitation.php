		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>ARAB INVESTMENT INTO 
                SCOTLAND </b></font><font face="Verdana" size="1"><br>
                <b>Given the impact of the global credit crunch on certain 
                sectors of the UK economy and the weakening of the pound against 
                the dollar, SWF’s are looking at Scotland for potential 
                investment opportunities<br>
&nbsp;</b></font><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1"><font color="#CC3300"><b>By Alan 
                Shanks<br>
                </b></font><br>
                <img border="0" src="invitation.jpg" align="right" hspace="8" vspace="6" width="93" height="108">The 
                high levels of liquidity in the GCC region over recent years 
                caused, to a large extent, by elevated oil prices, has meant 
                that governments have been able to build up significant budget 
                surpluses, some of which they reinvest into sovereign wealth 
                funds (SWFs) which in turn make investments in a wide variety of 
                assets, both at home and abroad. While definitive numbers are 
                hard to establish, estimates of $215bn of foreign assets being 
                acquired in 2007 alone do not seem unreasonable.<br>
                <br>
                <b>Targeting opportunities<br>
                </b>With oil reserves set to run out in the GCC this century the 
                governments are now taking overseas investments much more 
                seriously and have already played major roles in fund raising 
                exercises by banks worldwide, including the well publicised 
                examples of Barclays and Citigroup. In addition, we have 
                recently seen a number of high profile investments in more 
                diversified asset classes including the purchase of the iconic 
                QE2 (built in Scotland and recently arrived in Dubai) and the 
                acquisition of Manchester City Football Club by Abu Dhabi United 
                Group.<br>
                <br>
                Given the impact of the global credit crunch on certain sectors 
                of the UK economy and the weakening of the pound against the 
                dollar, SWF’s have looked at Scotland for potential investment 
                opportunities many of which currently offer good value for 
                money. <br>
                <br>
                So what are the particular attractions of Scotland to Arab 
                investors? The north most part of the United Kingdom has always 
                played a disproportionately major role in the economy of 
                Britain. As well as hosting some of the UK’s largest banks, the 
                country is also home to other financial service industries 
                including insurers, fund and private wealth managers. <br>
                <br>
                Aberdeen and its surrounding area have benefited from booming 
                North Sea oil and gas sector and many businesses have been very 
                effective in exporting these skills worldwide not least to the 
                Gulf. The strength of Scotland’s education system and in 
                particular its world-renowned universities has lead to a 
                thriving technology and innovation sector with numerous spin-out 
                and commercialisation projects currently under way. <br>
                <br>
                The financial services sector in Edinburgh has made the city one 
                of Europe’s and therefore the world’s largest financial centres. 
                The Scots are proud of their banking history and even issue 
                their own Scottish banknotes which are accepted elsewhere in the 
                United Kingdom. Experts predict that the financial services 
                industry accounts for around 8 per cent of Scottish GDP and that 
                their asset management industry controls around $880bn of assets 
                with many clients and investments coming from overseas. <br>
                <br>
                <b>Investments galore </b><br>
                <img border="0" src="invitation1.jpg" align="right" hspace="8" vspace="6" width="400" height="160">Scotland 
                is no stranger to investors from the Middle East. In May 2008 
                Leisurecorp, the Dubai World subsidiary which specialises in 
                developments and investments in the sports and leisure sector 
                purchased one of Scotland’s oldest golf courses, Turnberry. The 
                purchase price was around US$110mn and completed towards the end 
                of 2008. Leisurecorp believe that Turnberry has “vast potential” 
                and they plan on closing the course for redevelopment prior to 
                hosting The Open later this year. <br>
                <br>
                Another significant investment in Scotland has recently been 
                announced by Jumeriah Group. The company owns and operates four 
                hotels in Europe with the intention of making the fifth a six 
                star hotel in the Scottish city of Glasgow. The 26-storey 
                development will be in the city centre with 160 guest rooms and 
                85 serviced apartments.<br>
                <br>
                <b>Islamic Finance <br>
                </b>The mutual attraction between corporate Scotland and Middle 
                Eastern investors can be demonstrated by the willingness of 
                Scottish businesses and the public sector to consider, develop 
                and implement Sharia-compliant arrangements. <br>
                <br>
                At the beginning of 2008, Royal Bank of Scotland (RBS) funded 
                two deals in Scotland that were compliant with Sharia Law. The 
                first was the acquisition of Motherwell Bridge to Kuwait Finance 
                House and the second was the acquisition of Downhole Products by 
                Varel International Energy Services. Such is the interest in 
                Islamic Finance (and in keeping with Scotland’s progressive and 
                innovative approach to banking) bodies such as the Islamic 
                Finance Council UK (IFC) have been established to promote the 
                industry. <br>
                <br>
                All of this demonstrates Scotland’s commitment towards cementing 
                its role as an international financial centre. The Scottish 
                government has even been linked to investigating the issue of 
                Islamic bonds to fund infrastructure projects. The professional 
                services sector in Scotland has also received a boost as a 
                result of this activity with a range of advisers finding new 
                markets both at home and in the Middle East. This has led 
                businesses such as HBJ Gateley Wareing to becoming the first 
                Scottish law firm to set up an office in Dubai. We should expect 
                to see more Scottish firms to follow. <br>
                <br>
                <b>Looking ahead<br>
                </b>With the current economic crisis and the slide in oil prices 
                many people are arguing that 2009 may lead to a period of 
                retrenchment in the GCC with a resultant decline in interest in 
                the acquisition of international assets. However, while the 
                average price of oil has fallen around 49 per cent in 2008, the 
                dollar has risen by 27 per cent against the pound for the same 
                period. Coupled with a 16 per cent fall in United Kingdom 
                property prices this still leaves the SWF’s in a strong 
                position. As the SWF’s are fairly liquid they often require 
                little in terms of debt financing and as the United Kingdom 
                falls further into recession we could see some great bargains 
                which will attract the SWF’s to “cherry pick” Scottish assets. 
                Rumours continue to link a Dubai SWF with the purchase of 
                Edinburgh’s Princes Street with the view of creating one of the 
                world’s most prestigious shopping areas. Who would bet against 
                this or similar deals coming to fruition. <br>
