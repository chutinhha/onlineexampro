                </b><b>
            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">Service 
            is the key</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>Janet Turner, director of sales and marketing, Crowne Plaza, 
            Muscat speaks exclusively to OER about the soon to be introduced 
            Personal Hotelier programme</b><br>
            <br>
            <b>
            <img border="0" src="hos.jpg" align="left" hspace="8" vspace="6" width="200" height="227">What 
            is the Personal Hotelier programme?</b><br>
            It is a programme that is being introduced in Crowne Plaza Hotels in 
            the Middle East and Africa. The service will be offered to guests 
            staying in our Crowne Club rooms. It provides guests with a single 
            touch point for all his needs. It is a unique service offered to the 
            discerning business travelers within the region. The personalised 
            attention starts from the date that the guest makes a booking with 
            us, our personal hotelier will call and introduce himself to the 
            guest as soon as the reservation is made. The same person will then 
            reconfirm the booking to the guest prior to arrival, meet the guest 
            on arrival, call him during the stay to check for his comfort and be 
            present when the guest departs from the hotel. We are introducing 
            the service from February 1, 2009. <br>
            <br>
            <b>Why has Crowne Plaza launched this programme? </b><br>
            Personal Hotelier is being launched as an enhancement of our 
            existing guest service programme. It is designed to make our guests 
            feel valued and allows them to get on with business quickly, 
            efficiently and with minimum stress. In today’s challenging business 
            environment, travelling can be an extremely taxing and so the 
            personal hotelier is like having a personal assistant where-ever you 
            travel. <br>
            <br>
            <b>Has any market study been done to arrive at the programme? </b>
            <br>
            InterContinental Hotels Group have tested the personal hotelier 
            concept by running a pilot programme at the Crowne Plaza Dubai for 
            three months. The results were fantastic. On departure each of these 
            guests were asked to complete a questionnaire telling us what they 
            felt about the new service. 85 per cent of the guests told us that 
            they had made use of the service during the stay. 90 per cent of the 
            guests said that the service was efficient and useful. While 90 per 
            cent of our guests shared that the availability of a personal 
            hotelier at a hotel would encourage them to book with the hotel 
            again. <br>
            <br>
            <b>Will the programme force you to recruit new staff?</b><br>
            No, we are using our staff who currently work in guest relations and 
            the front office. This is a great opportunity for them to learn new 
            skills and interact with our guests on a personal level. We will 
            make the personal hoteliers easily identifiable for our guests by 
            providing them with an individual uniform which will make them stand 
            out from our other staff. <br>
            <br>
            <b>How will you be promoting this service?</b><br>
            There is an extensive marketing campaign behind the personal 
            hotelier service. The service will be promoted across the region 
            with advertisements in leading business publications. It will also 
            be featured on many websites such as CNN and Google. Each hotel has 
            been provided with specified marketing tools which they will use 
            internally to promote the personal hotelier service.<br>
            <br>
            The service will be on offer from February 1, 2009 for customers 
            staying in our club rooms. The process starts from the time that a 
            guest makes a reservation on the club floor. The idea is to make a 
            guest feel at home and create loyalty towards the brand. Close to 45 
            per cent of our business comes from corporate travellers and with 
            the Personal Hotelier scheme we are making that extra effort to make 
            him comfortable. </font></p>

