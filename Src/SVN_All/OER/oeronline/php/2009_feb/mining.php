		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Wealthy prospects</font></b><font face="Verdana" size="1"><br>
                <b>With the policy of economic diversification of the national 
                economy gaining urgency in the wake of the current financial 
                crunch, the mining sector could not hope for a more opportune 
                moment to draw attention to itself, writes Visvas Paul D Karra</b><br>
                <br>
                Oman’s mining history dates back to pre-Islamic days when copper 
                was mined and sent all over the world. The country was then 
                known as Majan or the land of copper. Cut back to the present 
                days and you will realise that the Sultanate of Oman is still 
                sitting on a ‘gold-mine’ of minerals. Despite the prices of 
                commodities having gone down, private and public sector 
                investments in this largely untapped non-hydrocarbon resource is 
                expected to draw benefits for the country as a whole.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
                <img border="0" src="mining3.jpg" width="400" height="253"><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1"><br>
                Some of Oman’s main mineral resources include chromite, 
                dolomite, zinc, limestone, gypsum, silicon, copper, gold, cobalt 
                and iron. Several industries have grown up around them as part 
                of the national development process which, in turn, have boosted 
                the mineral sector’s contribution to the nation’s GDP as well as 
                providing jobs for Omanis.<br>
                <br>
                But for a country blessed with bountiful mineral wealth – 
                surpassing even that of its oil-rich partners, Oman progress in 
                this direction is very slow. The focus remained on traditionally 
                mined deposits – marble, gypsum, limestone, industrial salt, 
                chromites, gold, and building materials – with little evidence 
                that other veritable hoards would be tapped anytime soon. <br>
                <br>
                <img border="0" src="mining2.jpg" align="right" hspace="8" vspace="6" width="175" height="127">The 
                setting up of the Mineral Development of Oman (MDO) in March 
                2008, however, could prove to be the bedrock upon which the 
                mineral sector could be firmly built upon. MDO has a range of 
                partners, including private sector firms and the Directorate 
                General of Minerals, to encourage and direct investments in 
                industrial-grade mineral mining in the country. The Directorate 
                General is a government body charged with ensuring the 
                sustainable development of Oman’s mineral resources, and its 
                participation is indicative of the government’s enthusiasm, and 
                should ensure that it follows the diversification plans. MDO has 
                brought a new dynamism to the mining sector, and has lost little 
                time in bringing its weight to bear on the sector. On June 10, 
                the group’s first special purpose vehicle, Manar Carbonates was 
                launched. Manar will produce ground calcium carbonates and 
                precipitated carbonates from the Sultanate’s ample limestone 
                deposits.<br>
                <br>
                <b>Mineral City</b><br>
                Setting the pace for the mining sector is Takamul Investment 
                Company, a majority Omani government-owned investment vehicle, 
                which has an ambitious plan to establish a ‘Minerals City’ in 
                the Sultanate to serve as a hub for a number of minerals based 
                downstream processing projects.<br>
                <br>
                The Mineral City will initially house three major minerals 
                derivatives projects which will begin their operations within 
                the next three years. These will be a magnesium-ferrosilicon 
                project (estimated cost: $250mn); a 40,000-tonne capacity 
                silicon carbide processing facility ($40mn); and a $450mn 
                project for a salt/soda ash project.<br>
                <br>
                The magnesium-ferrosilicon project will be developed in a joint 
                venture with a foreign partner while the salt/soda ash project 
                is planned in partnership with the Tata group, the Indian 
                business conglomerate. Further, SNAM Abrasives of India along 
                with Takamul will develop a 40,000-tonne capacity silicon 
                carbide processing facility.<br>
                <br>
                Details of when and where this mineral city will become 
                operational are still not available. Meanwhile, officials of the 
                ministry of commerce and industry, which has a director general 
                of minerals also remained tightlipped and have not divulged any 
                details on what their role is in developing this Mineral City.<br>
                <br>
                <img border="0" src="mining.jpg" align="left" hspace="8" vspace="6" width="150" height="300"><b>Increase 
                in exports</b><br>
                In 2007 export of mineral products was about $1.17bn while the 
                value of base metals and articles manufactured from base metals 
                reached about $310mn. There was a significant increase in 
                mineral production in 2007 as compared to 2006. Exports of 
                chromium ores and concentrates increased by 634 per cent to 
                about $52mn; while that of copper ore went up by about 300 per 
                cent; gold also increased by about 140 per cent; and sulfur by 
                an estimated 25 per cent. The significant increases of copper 
                ore were attributed to a full year of production at the new 
                copper mines of National Mining Co.<br>
                <br>
                “While it is true that minerals will supplement the oil and gas 
                revenues in Oman, we have to be really optimistic to feel 
                buoyant about mines and minerals in Oman at this juncture 
                because there is a steep drop in commodity prices all over the 
                world. But it is a storm that we can hopefully weather,” says 
                Hafidh Soud Al Busaidi, CEO of National Mining Company (NMC), 
                the copper mining in the Sultanate.<br>
                <br>
                Established in 1998, NMC was able to realise actual revenues 
                only in the last few years with its production reaching upto 60 
                000 tons of concentrate in 2008. The company, which has copper 
                mines at Shinas and Hatta, has also found copper deposits at 
                Ghuzayn. National Mining, part of the MB Holding Group, is 
                planning to do more mineral exploration in other areas for 
                copper, gold and other minerals as well. Presently, NMC produces 
                around 180-200 tons per day of copper concentrate at its plant. 
                This is equivalent to about 50 tons of pure copper per day. <br>
                <br>
                <img border="0" src="mining1.jpg" align="right" hspace="8" vspace="6" width="174" height="348">According 
                to Al-Busaidi, mining has a bright future and holds a lot of 
                potential. But it is difficult to make profit at the present 
                juncture due to the low commodity prices, he says while pointing 
                out that mining is not for the ‘weak at heart’. “The government 
                should differentiate between the serious and non-serious mining 
                companies and support serious people like our company. Despite 
                the drop in commodity prices and demand for our product, we have 
                so far not laid off any people. There are nearly 300 Omani 
                employees who are dependent upon us and we have a social 
                responsibility towards them,” underscores Al-Busaidi while 
                talking about the present market conditions.<br>
                <br>
                <b>International exposure</b><br>
                The setting up of Vale Oman, a subsidiary of Brazil-based Vale 
                International, a new entrant into the mineral market of Oman has 
                put the Sultanate firmly on the mineral map of the world.<br>
                <br>
                Vale Oman is constructing a pelletising plant in Sohar for 
                production of 9Mt per year of direct reduction pellets and a 
                distribution centre with capacity to handle 40Mt per year. The 
                project is planned to kick start by the end of 2010. The total 
                capital expenditure budget for Vale’s project is $1.35bn, with 
                investments of $458mn for 2009. Representatives of Vale in an 
                email statement say that since the port of Sohar is a deep water 
                harbour and strategically located along the busy corridor of the 
                shipping lanes, it matches with Vale’s plans for growth.<br>
                <br>
                The plant in Sohar is expected to be commissioned by the end of 
                2010. Officials are hopeful that the high-grade pellets from 
                their Sohar plant will supply the direct reduction facilities of 
                the steel industry in the Middle East, North Africa and other 
                relevant markets.<br>
                <br>
                Vale has signed a framework agreement with Oman Shipping Company 
                (OSC) to lease four giant ships to transport the iron ore from 
                the extraction areas in Brazil to their plant in Sohar Port. The 
                agreement was signed during the visit of HE Ahmed bin Abdulnabi 
                Macki Minister of National Economy &amp; Deputy Chairman of the 
                Financial Affairs &amp; Energy Resources Council; and Maqbool bin 
                Ali bin Sultan, Minister of Commerce and Industry and Chairman 
                of Oman Oil company (OOC), in November. OSC will build four 
                giant ships to transport the iron ore from Brazil to Vale’s 
                plant in Sohar.<br>
                <br>
                Apart from the pelletising plant, Vale has employed a geologist 
                to work in their Oman Office to do exploration work for 
                minerals. He will be discussing with local authorities to make 
                an agreement with them in order to better understand which 
                minerals are available here and to prepare a master plan for 
                potential development.<br>
                <br>
                <b>Infrastructure and logistics</b><br>
                The growing demand for Oman’s minerals has prompted many 
                companies to float either subsidiaries or joint ventures for 
                exploration and mining of the vast resources available in Oman. 
                Dr V Rangaiah, general manager, Al Nebras International Services 
                Co (NISCO), says that there is a lot of scope for local 
                entrepreneurs to have joint ventures with foreign investors for 
                excavating and exporting of minerals because Oman presently 
                lacks the type of technology and skilled labour to launch 
                big-time into the mining sector. NISCO is the marketing arm of 
                the Al Amri Group which owns the Muscat Chromite Company.<br>
                <br>
                Muscat Chromite produces over 50,000MT of chrome ore per month 
                and NISCO markets this in India and China. The company achieved 
                a turnover of nearly $20mn in the year gone by. NISCO also 
                markets manganese, iron, lime stone and gypsum are other 
                products from their partner mines in Iran. Oman’s mineral 
                resources could provide the second largest revenues, next to the 
                oil &amp; gas sector but the government has to be choosy in 
                identifying companies and entrepreneurs who have the capacity 
                and capability in this line, feels Dr Rangaiah.<br>
                <br>
                Since there is so much potential in the mineral industry, the 
                government should also provide the required infrastructure and 
                logistic support to develop this sector. A pressing need of the 
                hour is a dedicated sea port for exporting minerals. This could 
                be set up in the Batinah region as the existing ports are not 
                equipped for mining operations, Dr Rangaiah opines.<br>
                <br>
                Value addition mechanisms like beneficiation and processing 
                plants combined with sustainable mining leads to not only 
                immediate economic benefits for the people but will bring in 
                long term benefits. </font> 
