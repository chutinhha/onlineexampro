		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">DOING BUSINESS IN 
                TURKEY</font></b><font face="Verdana" size="1"><br>
                <b>Turkey offers significant opportunities to foreign investors 
                with its geographically ideal position as a gateway between 
                Europe, the Middle East and central Asia<br>
                <br>
                <font color="#CC3300">By Sridhar Sridharan</font><br>
                </b><br>
                <img border="0" src="view.jpg" align="right" hspace="8" vspace="6" width="102" height="119">Since 
                hospitality and tolerance are the traditional cornerstones of 
                the Turkish way of life, the country is open to foreign 
                investors with many attractions to offer, such as a large and 
                growing domestic market, a mature and dynamic private sector, 
                and a liberal and secure investment environment. In addition, 
                the availability of high quality and cost-effective labour 
                force, customs union with EU countries, a well developed 
                infrastructure, an institutionalised economy and a competitive 
                tax system makes Turkey attractive to investors.<br>
                <br>
                <b>Economic overview<br>
                </b>Turkey offers a huge and dynamic domestic market to foreign 
                investors with a population of more than 70mn, of which 63 per 
                cent is below the age of 35. Turkey’s dynamic economy is a 
                complex mix of modern industry and commerce along with a 
                traditional agriculture sector that still accounts for more than 
                35 per cent of employment. It has a strong and rapidly growing 
                private sector, yet the state still plays a major role in basic 
                industry, banking, transport, and communication. The largest 
                industrial sector is textiles and clothing, which accounts for 
                one-third of industrial employment. The existence of more than 
                20,000 (as of July 2008) foreign capital firms, including many 
                regional headquarters, ensures a stable and reliable investment 
                environment in Turkey. The value of Foreign Direct Investment 
                currently stands at about $85bn. Privatisation proceeds are 
                currently approaching $21bn. Oil began to flow through the Baku-Tblisi-Ceyhan 
                pipeline in May 2006, marking a major milestone that will bring 
                production upto one million barrels per day from the Caspian to 
                the market.<br>
                <br>
                The investment climate has improved through a series of changes 
                in the legislation in recent years. These changes include 
                eliminating the requirement to comply with bureaucratic 
                formalities for foreign investors, simplifying the registration 
                procedures for setting up businesses and announcing rules to 
                accelerate the work permit procedures for foreign employees. 
                Together with the latest improvements, the existing legal 
                environment assures:</font><ul>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  A liberal and investor friendly foreign investment regime,</font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  A liberal exchange control regime,</font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Many bilateral and multilateral agreements (UN, NATO, OECD, 
                  WHO, GATT, WTO, ILO, IMF, ECO, OIC, BSEC, IDB, ICSID, and 
                  others),</font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  And an extensive network of Double Tax Treaties.</font></li>
                </ul>
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>Incentives</b><br>
                Under the domestic tax legislation, there are various tax 
                incentives that are aimed at encouraging investments, especially 
                for priority development regions, small and medium size 
                enterprises (“SME’s”), technology development zones, research 
                and development (“R&amp;D”) activities and exports.<br>
                <br>
                <img border="0" src="view1.jpg" align="left" hspace="8" vspace="6" width="367" height="255">Investment incentives include the exemption of customs duties on 
                imported machinery and equipment with an Investment Certificate, 
                and VAT exemption on imported or locally delivered machinery and 
                equipment. As a corporate tax incentive, 100 per cent of R&amp;D 
                expenditures may be deducted from the corporate tax base, 
                provided certain conditions are fulfilled. This does not affect 
                the ordinary expense recognition of R&amp;D expenditures of 
                companies.<br>
                <br>
                For the technology development zones, in addition to VAT, income 
                tax and customs duty exemptions, infrastructure facilities are 
                also provided. Incentives are also available to exporters. The 
                main incentives cover support for R&amp;D activities, market 
                research, participation in exhibitions and international fairs, 
                expenditures for fairs, expenditures for patents, trademarks and 
                industrial design. Finally, Turkey’s Free Trade Zones (“FTZ’s”) 
                offer an attractive investment climate to investors. Both local 
                and foreign companies can benefit equally from the incentives 
                granted in the Free Trade Zones.<br>
                <br>
                <b>Types of business entities </b><br>
                The most common types of companies in Turkey are joint stock 
                companies and limited liability companies that are both known as 
                capital companies.<br>
                <br>
                <b>Limited liability companies</b><br>
                Partners of such companies may either be natural persons or 
                legal entities. Such companies may be established with the 
                participation of a minimum of two partners and a maximum of 50 
                partners. The minimum capital requirement is Turkish Lira (TRY) 
                5,000 (approximately $3,300) and restrictions on the right to 
                transfer shares is applicable.<br>
                <br>
                <b>Joint stock companies</b><br>
                A joint stock company should have a specific business title and 
                a capital which covers an amount that has been determined 
                before. The structure and organisation of joint stock companies 
                are subject to the regulations set forth in the Turkish 
                Commercial Code (“TCC”).<br>
                <br>
                <b>Branches of foreign companies</b><br>
                The establishment of a branch office must be approved by the 
                Ministry of Industry and Trade. Following the receipt of this 
                approval, the branch office must register with the competent 
                Trade Registry. In accordance with the Direct Foreign Investment 
                Law, the General Directorate of Foreign Investment must be 
                informed once the branch is established. The company must 
                appoint a representative who can represent the foreign entity in 
                Turkey before all public and private authorities.<br>
                <br>
                <b>Main structures used by foreign investors</b><br>
                The forms most commonly used by foreign investors are limited 
                liability companies, joint stock companies and branch offices. 
                All these types of entities are subject to a corporate tax of 20 
                per cent. Additionally, the domestic dividend withholding tax 
                rate and the withholding tax rate on branch remittances is 15 
                per cent.<br>
                <br>
                <b>Corporate income tax<br>
                </b>Companies whose legal or business headquarters are located 
                in Turkey or whose operations are managed in Turkey are subject 
                to corporation tax on their worldwide income. In principle, 
                corporate income is subject to corporate tax at 20 per cent. 
                Advance corporate payments at the rate of 20 per cent are also 
                applicable and must be submitted every quarter.<br>
                <br>
                <b>Withholding taxes<br>
                </b>Certain income and gains of non-resident corporations are 
                subject to withholding taxes (WHT). In the presence of a Double 
                Tax Agreement (DTA) the WHT levied on such payments may be 
                reduced or even decreased to zero. The withholding tax rates on 
                some important income items are as follows:</font><ul>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Dividends: 15 per cent </font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Remittance of branch profits to the foreign headquarters: 15 
                  per cent </font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Interests payments<br>
&nbsp; &gt; from loans granted by foreign financial institutions: 0 per cent <br>
&nbsp; &gt; from loans granted by other foreign entities: 10 per cent </font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Royalties: 20 per cent </font></li>
                  <li> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                  Professional fees: 20 per cent</font></li>
                </ul>
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>Individual income tax<br>
                </b>Individual income and gains are subject to income tax at 
                progressive tax rates which vary between 15 per cent and 35 per 
                cent and are calculated on a cumulative basis. Additionally, 
                certain individual (both for resident and non-residents) income 
                and gains are subject to withholding taxes (WHT). In the 
                presence of a Double Tax Agreement (DTA) it might be possible to 
                reduce the tax rates or exclude the taxation in Turkey for the 
                income and earnings derived by non-resident individuals.<br>
                <br>
                <b>Other Taxes<br>
                </b>The other significant taxes to be considered by a foreign 
                investor include VAT (Value Added Tax), Special Consumption Tax, 
                Banking and Insurance Transaction Tax (for certain financial 
                activities) and Stamp Duty.<br>
                <br>
                <b>Double Tax Agreement</b><br>
                Oman and Turkey signed a Double Tax Agreement on May 31, 2006 
                but it has yet to come into force. <br>
                <br>
                <b>Social Security</b><br>
                All employees of Turkish private entities are covered by a 
                national social insurance system that includes work-related 
                accidents and illnesses, pregnancy, disability and death. It 
                also provides for retirement benefits. Employers and employees 
                pay monthly contributions at varying percentages calculated on 
                the gross salary up to a ceiling stated in the Social Security 
                Law.<br>
                <br>
                <b>Exchange Controls</b><br>
                Turkey has a liberal foreign exchange regime, which allows local 
                foreign exchange accounts. Companies may transfer profits, 
                provided the company submits to its bank the approved tax 
                statement and its tax accrual and payment slips. This law also 
                guarantees the remittance of the proceeds from the liquidation 
                of an investment. Fees and royalties from management agreements, 
                technical services agreements and license contracts may be 
                remitted abroad. Foreign investment partnerships and funds may 
                invest in Turkish securities and freely remit dividends, 
                interest, profits and capital. Turkish resident companies are 
                now allowed to grant loans to their related parties which reside 
                abroad.<br>
                <br>
                <b>Import Regulations</b><br>
                In general, there are no import restrictions on goods imported 
                into Turkey, except for some minor restrictions. Import of 
                certain goods like films and videos require permission from 
                relevant ministries. The undersecretary of foreign trade 
                maintains a list of goods that require exclusive permission. For 
                goods that are imported into Turkey from countries other than 
                the European Union, customs duties and charges are applicable in 
                conformity with the provisions of the customs union. Regardless 
                of the origin of goods, whether from EU or non-EU countries, 
                value added tax at 18 per cent over CIF value, including other 
                duties and funds, is collected. Investment goods like machinery 
                and equipment are exempt from VAT and customs duties, provided 
                an investment certificate is obtained from the undersecretary of 
                treasury. In summary, Turkey’s solid economic fundamentals 
                marked by strong economic growth and foreign direct investment 
                provide investors and exporters with innumerable business 
                opportunities. <br>
