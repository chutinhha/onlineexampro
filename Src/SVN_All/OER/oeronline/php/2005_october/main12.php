<img border="0" src="tec1.jpg" align="left" hspace="5" vspace="2" width="160" height="264">W</b></font><font face="Verdana" size="1">e 
identify Internet and mobile as a means of communication with our personal and 
professional associates besides a mode of facilitating our official duties in an 
efficient manner. But have you ever thought that these tools could also 
revolutionize the way we manage our home appliances? There was a time not so 
long ago when it was considered that Internet and mobile devices had no 
connection with home appliances. But LG Electronics has changed all that. It has 
developed and commercially introduced a revolutionary technology called ‘Home 
Network’ which does exactly what we are talking about. <br>
<br>
Home Network is an emerging trend in home appliances that features built-in 
communication capabilities, providing added convenience and safety at home, and 
more time for leisure. Away from home, one can now operate the appliances and 
even monitor the progress of one’s home, something that was in the realm of 
dreams earlier. This proves that Internet is not just for our offices. It's for 
our life! At LG, these home appliances with built-in Internet functions are 
called ‘Dream Home Products’.<br>
<br>
<img border="0" src="tec2.jpg" align="right" hspace="5" vspace="2" width="160" height="235">Technically 
speaking, Dream Home is an inter-home appliance network that enables 
communications and control using LG's own communications protocol LNCP (Living 
Network Control Protocol). LNCP is an optimized communications protocol for home 
appliances where the amount of data communicated is relatively small. <br>
<br>
Some examples of Home Network products include an Internet refrigerator that 
allows users to shop online, make video phone calls and watch TV broadcasts; the 
Internet enabled washing machine can download pre-programmed washing schemes for 
different laundry loads; sitting in office you can switch on the air conditioner 
in your house so that by the time you come back, the house has air temperature 
as per your requirements; through Video On Demand service a user can freely 
choose and watch movies on various display equipment without visiting video 
rental stores in person; it allows to monitor and control networked digital 
equipment such as lighting fixtures, electrical window, electrical curtain and 
door lock etc. In short, it’s a new world out there. <br>
<br>
The home network system operates the e-Home in three developmental stages. The 
first stage that consisted of developing individual Internet-enabled products 
was done by LG in 2000. The next stage was the living network stage where 
individual products are connected via a network, remote home control and home 
automation. This has already been technologically accomplished. With the 
increase in broadband networks and Internet penetration, living network products 
are selling briskly. <br>
<br>
The e-Homes stage is the last stage and offers complete home management, such as 
self-diagnosis and automatic reporting. It provides home safety, advanced home 
security, and home entertainment solutions. <br>
<br>
On the revenue front, as per LG, the home networking and residential gateway 
equipment market is estimated to reach around US $5.9 billion by the end of this 
year. Home networking services that deliver extra value based on new forms of 
connectivity, is estimated to reach over US $3 billion this year. <br>
<br>
“By 2005-end, over 100 million homes are expected to be using networked digital 
appliances and LG believes a large proportion of these will be in the Middle 
East and Africa,” said K H Kim, President, LG Electronics Middle East and Africa 
Operations. “Two to three years ago, when home networking products were unveiled 
commercially, people doubted their technological viability and marketability. 
Technology has evolved since then and LG has invested considerably in R &amp; D to 
develop home networking enabled products such as the Internet refrigerator, 
washing machine, air-conditioner and microwave, that are wired into the Internet 
and operate independent of each other.<br>
<br>
