revenues. Increasing worker productivity and innovation represent the best paths 
to business growth,” said Ramin Attari, Vice President Middle East, Nortel. “At 
Nortel, our focus is providing multimedia communication capabilities that 
increase employee productivity and drive innovation. Our new IP multimedia 
solutions are designed to make it possible for employees to collaborate and work 
more effectively. The results are a more collaborative environment, increased 
worker productivity and the ability to be much more responsive to customer 
needs.” These new solutions include: the Nortel IP Phone 1100 Series, which 
features advanced capabilities including high-resolution displays for graphical 
content presentation along with USB and Gigabit Ethernet capabilities. Nortel is 
introducing three new models: the IP Phone 1110, IP Phone 1120E, and IP Phone 
1140E, each designed to deliver sophisticated telephony features and 
applications.<br>
