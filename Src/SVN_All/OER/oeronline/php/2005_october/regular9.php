Commercial Companies in Oman<br>
<br>
</font></b><font face="Verdana" size="1"><b>Michael Earley focuses on the 
different structures of commercial companies in Oman</b><br>
<br>
</font><font face="Verdana" style="font-size: 14pt">
<img border="1" src="lega.jpg" width="120" height="142" align="left" hspace="5" vspace="2">O</font><font face="Verdana" size="1">man 
offers considerable number of corporate forms in which to structure a commercial 
company. Specifically, Sultani decree 04/74 promulgating the Commercial 
Companies Law of Oman as amended (CCL) recognises six different forms for 
commercial companies in Oman. These are the limited liability company, the joint 
stock company (both closed and public), the general partnership company, the 
limited partnership company, and the holding company.<br>
<br>
In addition to the provisions of the CCL, foreign entities seeking to do 
business in Oman must also conform to the provision of Sultani decree 102/94 
promulgating the Foreign Capital Investment Law (FCIL). In examining these 
companies, and for the purposes of this article, we will assume that the 
companies described below have some amount of foreign participation.<br>
<br>
<b>The Limited Liability Company (&quot;LLC&quot;)</b><br>
In an LLC, the liability of individual partners is limited to the nominal value 
of their contribution to the capital of the company. The memorandum and articles 
of an LLC are contained in one instrument entered into by the partners and 
referred to as the &quot;Constitutive Contract&quot; of the company.<br>
<br>
The minimum number of partners in an LLC is two, up to a maximum of forty. The 
minimum capital requirements for a mixed LLC (i.e one in which there is a 
foreign shareholding, as noted above) is RO150,000. This capital is divided into 
equal shares of any size. Any proposed transfer of shares to a third party is 
subject to the pre-emptive purchase rights of the other partners.<br>
<br>
The authority to manage an LLC vests entirely in the natural person(s) appointed 
by the partners as statutory manager(s) subject to certain statutory 
constraints, and except to the extent specific executive powers are reserved to 
the partners themselves in the constitutive contract or by subsequent partners' 
resolutions. There is no board of directors of an LLC. Instead, strategic 
decisions are made by the partners with the statutory manager having day-to-day 
control.<br>
<br>
The formalities relating to partner meetings are significantly less stringent 
than those which apply to a joint stock company. It is also important to note 
that shares in an LLC are not represented by negotiable instruments and 
consequently cannot be charged.<br>
<br>
Save for certain types of business which are restricted to Public Joint Stock 
(&quot;SAOG&quot;) and Closed Joint Stock (&quot;SAOC&quot;) Companies such as banking, insurance, 
shipping, etc., an LLC may be authorised to carry out any type of business. 
However, certain activities may require that a licence be obtained for that 
activity before the company can be registered. <br>
<br>
<b>Closed Joint Stock Companies (SAOC) </b><br>
This vehicle is similar to an English unlisted plc or an English limited 
company. It is managed by a board of directors, has share capital of a certain 
nominal value (or different classes of share capital), and has a memorandum and 
articles of association. <br>
<br>
Whist it is unlisted in the sense that its shares are not quoted, transfers of 
shares are conducted on the floor of the Muscat Securities Market (&quot;MSM&quot;) and 
shareholder registers are maintained by the Muscat Depository and Securities 
Registration Company SAOC in the same way as for publicly listed companies. 
Shares are represented by numbered transferable certificates.<br>
<br>
The minimum capital of an SAOC is Rial Omani five hundred thousand (RO500,000) 
and the minimum number of shareholders is three. The board of directors conducts 
business, with certain business reserved by law or by the articles to the 
shareholders in ordinary or extra-ordinary general meetings.<br>
<br>
Unlike an SAOG, promoters in an SAOC are not restricted from disposing of their 
shares prior to the publication of two annual audited balance sheets of the 
company. This means in practice that the shares may be mortgaged at any time to 
secure any liabilities of the company.<br>
<br>
<b>Public Joint Stock Companies (SAOG)</b><br>
These are companies whose shares are listed on the Muscat Securities Exchange. 
All SAOG companies must be listed. The other features of an SAOG are the same as 
those for an SAOC, save that the minimum capital requirement is Rial Omani two 
million (RO2,000,000). On listing the company, the maximum shareholding of the 
promoters is 60 per cent, requiring 40 per cent of the shares to be offered to 
the public. <br>
<br>
The total shares owned by the promoters may not be less than 30 per cent of the 
share capital. It should also be noted that if an SAOC is first formed and is 
then subsequently floated as an SAOG, there is a requirement for three years' 
audited balance sheets to have been produced by the company prior to 
conversion/promotion.<br>
<br>
There is a restriction on promoters disposing of their shares in an SAOG prior 
to the publication of two audited balance sheets of the company (except in the 
case of a transfer to a fellow promoter). This means in practice that the shares 
cannot be mortgaged within this period.<br>
<br>
<b>General Partnership Companies (&quot;GPC&quot;)</b><br>
In a GPC, the liability of individual partners is unlimited. The memorandum and 
articles of a GPC are contained in one instrument entered into by the partners 
and referred to as the &quot;Constitutive Contract&quot; of the company.<br>
<br>
The minimum number of partners of a GPC is two. The minimum capital for a mixed 
GPC is Rial Omani one hundred and fifty thousand (RO150,000). This capital may 
be divided into equal shares of any size. Partners in a GPC who are not managers 
are precluded from management activities and they may not act in the name of the 
partnership. <br>
Limited Partnership Company (&quot;LPC&quot;)<br>
<br>
In a LPC, there are two types of partners. The first group consists of general 
partners whose liability is not limited. The second group consists of the 
limited liability partners whose liability is limited to the nominal value of 
their contribution to the capital. The memorandum and articles of an LPC are 
contained in one instrument entered into by the partners and referred to as the 
&quot;Constitutive Contract&quot; of the company.<br>
<br>
The minimum number of partners of an LPC is two. The minimum capital for a mixed 
LPC is Rial Omani one hundred fifty thousand (RO150,000).<br>
<br>
The limited partner who participates in the management of the LPC or who acts in 
the name of the LPC is liable as a general partner for the obligations arising 
from his acts and may be held responsible as a general partner for all or any of 
the LPC's other debts according to the importance and frequency of his acts and 
according to the reliance of third parties in good faith on him on account of 
those acts. However, a limited partner is not deemed to be participating in the 
management of an LPC by reason of his mere participation in the internal 
management through the exercise of rights provided by law or the LPC's 
constitutive contract.<br>
<br>
<b>Holding Companies</b><br>
A holding company is a commercial company that has financial and management 
control over one or more companies that become subsidiaries of it through its 51 
per cent or more ownership of the shares of that company. These may take the 
form of either a limited liability company, or a joint stock company. The 
minimum capital requirement is Rial Omani two million (RO2,000,000), regardless 
of the form of company the holding company takes.<br>
<br>
<b>Decisions, Decisions</b><br>
Those wanting to form a commercial company in Oman may choose the type they wish 
to establish, provided that the provisions of the CCL and FCIL are followed. In 
addition to these forms of commercial companies, foreign entities in Oman may 
establish branch offices or representative offices. However, the freedom to 
carry out commercial activities under these forms is limited considerably by the 
law.<br>
