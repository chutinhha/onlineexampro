<p align="justify" dir="ltr"><font face="Verdana" size="2" color="#C0C0C0">:: Corporate 
Profile<br>
</font><font face="Verdana" size="4" color="#FF0000">
<br>
Crowning Glory<br>
<br>
</font><font face="Verdana" size="1"><b>OER traces the remarkable trajectory of 
Poly Products LLC, a company that has set new benchmarks in polyurethane foam 
products, some of which now enjoy a truly international brand equity</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">
<img border="1" src="raha.jpg" width="98" height="240" align="left" hspace="5" vspace="2">It 
all began with a dream. With an investment of RO100,000, and a team of five 
people, exactly! And a factory area of 2,000 square metres! Poly Products LLC 
has traveled a real good distance since those days in the year 1979 when the 
company, began its manufacturing operations in Darsait.<br>
<br>
Today, the company has a capital of RO1,000,000; operates from a sprawling 
manufacturing complex in Rusayl, with a factory area of 45,000 square meters, 
and has a team of 450 people. And, the company’s turnover? Well, the current 
turnover is $20 million. The company manufactures and markets a wide range of 
products like mattresses and beds, pillows and comforters, sofa sets, 
polyurethane foam and polyester fibre. <br>
<br>
The company’s products cater to the needs of both the domestic household segment 
as well as the institutional segment of hotel chains.<br>
<br>
However, any mention of the company will be incomplete without Raha, its 
flagship brand. Raha is not only a highly reputed name in the Middle East but 
also enjoys a brand equity that is truly international, with customers across 
the Middle East, Europe and South-east Asia. Says Anwar Ahsan, General Manager, 
Poly Products: “Customers identify Raha with consistent quality and a reasonable 
and affordable price. Customers believe that Raha products are not only the 
cheapest but provide good value for money as well, considering their usable life 
period. Raha, as a brand, enjoys high brand equity. There is also a national 
pride attached to the brand ‘Raha’ as it is a well-known Omani brand in the 
other GCC states, as well as in the other neighbouring Arab countries like 
Jordan, Syria etc.” <br>
<br>
So, what is the secret behind the company’s remarkable climb up the ladder over 
the years? Quips Ahsan: “Several factors have contributed to the company’s 
success. Our commitment to deliver quality products consistently has been a 
major factor for our success. With such a commitment, Raha, as a brand, got well 
established among the customers and when we expanded our product range from beds 
to bed linen to sofas with similar emphasis on quality, we could ride piggy-back 
on the Raha brand image that we had built up.”<br>
<br>
Spelling out the formula of his company’s accomplishments, Ahsan adds, “Our 
adherence to international standards and benchmarks is another factor that has 
helped us to become a nominated supplier of mattresses for some of the reputed 
hotel chains like Sheraton, Le Meridien, Shangri La and so on.”<br>
<br>
The company’s ability to attract, train and retain good talent is also a 
contributing factor. The average length of service of workers is 9.6 years while 
that of the staff members is 8.6 years.”<br>
<br>
Adds Ahsan: “Another major factor is the setting up of our own branch networks, 
with distribution and marketing infrastructure, in other GCC states, whereby we 
ensured availability of excellent local support and service that are easily 
accessible to our customers.”<br>
<br>
It has been a steady story for the company so far. A journey down the memory 
lane is truly revealing. “Initially when the company was set up in Darsait, we 
were involved in production of flexible foam and rigid foam insulation. In 1980, 
we began producing foam mattresses. Simultaneously we were also, in a small way, 
experimenting with the production of spring mattresses and in 1983, we moved 
into manufacturing of spring mattresses that would be retailed from furniture 
shops. In 1985, Oman’s first industrial estate was set up in Rusayl and the 
company moved to its new factory in Rusayl Industrial Estate. By the end of 
1985, production had stabilised and was in full swing,” informs Ahsan.<br>
<br>
“It was at this point of time that we started looking at the bigger picture, 
i.e., the export markets, as we felt that if at all we are to expand and grow, 
we need to look outside Oman also. We could see the potential of the GCC states 
and we started with the UAE market due to its proximity. We set up our own 
branch office in Sharjah in 1990 with the necessary infrastructure for 
marketing, warehousing and distribution of our products in the UAE. Similar 
branch offices were opened subsequently in Kuwait (in 1995), Saudi Arabia (in 
Riyadh and Jeddah in 1996) and Qatar (in 2004). This apart, we have also been 
marketing our products in countries like Bahrain, Cyprus, and Jordan etc., 
Today, more than 60 per cent of our sales turnover comes from export markets 
outside Oman.<br>
<br>
“While we were looking at export markets around mid-eighties, we were 
simultaneously engaging ourselves in expanding our product range and enhancing 
our production capacities. We moved into production of bed linen and soft 
furnishing items in 1986 and thereafter in 1987 we started producing polyester 
waddings, using polyester staple fibre as raw material. We then commenced 
production of sofas and upholstered furniture in 1992.” <br>
<br>
To add to pure commerce, there is another eye of the company that makes it truly 
unique: its indulgence in and promotion of sporting activities. Says Ahsan with 
a measure of pride and satisfaction: “Our company fosters the spirit of 
excellence in not only on what is done by its people within the company but also 
on what is done by its people outside the company. This is evident from the fact 
that seven of the cricket team members that had represented the country of Oman 
in the recently held ICC Trophy cricket tournament in Ireland, are from Raha. In 
fact, both the captain and the vice-captain of the Oman cricket team are from 
Raha.” <br>
<br>
What about the company’s plans to expand the production basket and venture into 
new areas? What about joint ventures or international JVs? Answers Ahsan: “In 
October 1999, we signed a licensing agreement with Simmons of USA, to be its 
sole licensee to manufacture and market the Simmons range of specialty sleep 
systems in the Middle East. Simmons, a company with a turnover of $800 million 
in the US, is one of the largest selling international bedding brands and their 
choice of Poly Products LLC as their licensee is an endorsement of the company’s 
strengths.<br>
<br>
“In August 2005, we formed a new company ‘Emporio Calia LLC’ as a joint venture 
with an Italian company called Calia Italia to manufacture and market high-end 
leather sofa sets and leather upholstered furniture throughout Middle East and 
India. Calia Italia is a $90 million turnover company, based in Italy, 
manufacturing and marketing the world’s finest leather sofa sets in exquisite 
and contemporary designs.<br>
<br>
“Efforts are also on to develop a new range of Health Smart products, which 
would be latex based beds and which would involve the usage of sanitized foam 
and fabric with anti-dust mite and anti-fungus properties,” informs Ahsan. </font></p>

