<p align="justify"><font face="Verdana" size="1"><b>Shell has been in Oman for 
over 60 years now. The Shell Representative Office Oman was established in 2002. 
What is the role of this Representative Office?</b><br>
As its name suggests, the role of our office is essentially a representative 
one. Before it was set up in 2002, the Managing Director of PDO, a Shell 
secondee, also used to represent Shell in the Sultanate. But the MD of PDO is 
primarily responsible to his board of directors, and it was sometimes not easy 
for him to wear two hats -- to represent Shell on the one hand and to be 
responsible for PDO on the other hand. So we took a decision to establish a 
separate representational office, with a separate Shell Country Chairman who 
could remove that ambiguity. <br>
<br>
Essentially, we are here to represent Shell in Oman, and I do that by 
representing Shell on the boards of the various joint ventures and companies in 
which Shell has an interest; so that’s PDO for upstream oil and gas, OLNG (Oman 
Liquefied Natural Gas) for downstream gas, and Shell Oman Marketing Co. for oil 
products, and of which I am also Chairman. <br>
<br>
But equally important is my responsibility to represent Oman within Shell. For 
example, one of the roles of the office is to take responsibility for 
coordinating any negotiations taking place with the government. It’s very 
important to ensure that our decisions are taken in the context of not just what 
is best for Shell but equally, what is best for Oman. <br>
<br>
<b>What difference has the Representative Office made to Shell’s overall 
operations in Oman, and to Shell Group’s association with the Sultanate of Oman?</b><br>
The main reason for setting up this separate office was to give unique 
representation in Oman without ambiguity. So if anybody from the government, 
other organization or a customer talks to me, they know that they are talking to 
Shell’s single representative in the country who can address whatever concerns 
or queries they may have about Shell. <br>
<br>
So, it provides clarity of relationships. I believe that the setting up of this 
office has resulted in much improved communication with our stakeholders - 
whether they be in the government, our partners, NGOs, customers or the general 
public. <br>
<br>
The other big benefit is that it has provided us a base from which we can run a 
whole gamut of social investment initiatives, which we feel are our duty to 
undertake as responsible corporate citizens of Oman.<br>
<br>
<b>As Shell Country Chairman and General Manager of Shell Representative Office 
Oman, what is your main role?</b><br>
Personally, my main responsibility is to communicate, and that means 
communicating with anybody in Oman whose lives we touch in one way or the other. 
I often remind my colleagues in Shell that every hour I spend talking to them is 
one less hour spent doing what I am here for: talking to the outside world! 
Essentially, you can summarise our corporate role as one of protecting Shell’s 
reputation in the country to ensure that Shell continues to be – and is 
recognized as being - a reliable and trustworthy partner of the Sultanate. <br>
<br>
<b>How does the Shell Representative Office in Oman add value to Shell’s 
interest in Oman and in particular to the businesses and operations of the joint 
ventures?</b><br>
At Shell, we attach a lot of value to transparency, and I think we add value by 
giving congruent messages. My fellow colleagues who also represent Shell on the 
boards of the different JVs come from different parts of the business; some from 
the upstream, some from downstream gas, some from downstream oil. <br>
<br>
What I can provide is a kind of ‘red thread’ through the fabric of Shell in 
Oman, ensuring that different parts of the group are not seen as essentially 
different organizations, and that we are seen as one entity. I need to be able 
to see the full picture; so, for example, if I find one business pursuing 
something that I feel is conflicting with what another business is doing, I can 
try and resolve that. <br>
<br>
The other thing that I do is to provide an early warning. If I see things 
potentially going wrong, I can alert the various parts of Shell that have the 
expertise to find solutions. <br>
<br>
Another of my responsibilities is to provide internal business assurance. Shell 
operates according to a set of business principles which have been in place 
since the 1970s and which have just been updated. Each year, one of my 
responsibilities is to provide (through quite a rigorous process) assurance that 
all of the business conducted by Shell in Oman has been carried out entirely in 
accordance with these principles. <br>
<br>
<b>What are the challenges that you face as the Shell Country Chair and how do 
you manage to overcome these challenges?</b><br>
Shell has been privileged to play a very major role in Oman’s development, and 
the oil and gas industry will remain a dominant force in the national economy 
for many years to come, despite the highly successful efforts the government is 
making to diversify the economy. Maintaining our objective of ‘preferred 
partner’ status is something that is not always easy. But ultimately, I believe 
Shell should not be judged by what I say or anybody else says, but by what we do 
and how we are seen to contribute. <br>
<br>
<b>Would you say that there is some confusion/lack of awareness about the role 
and functions of Shell Representative Office Oman in relation to Shell Oman 
Marketing Company?</b><br>
Well, I can’t deny that there is some confusion, even though we do actually have 
different corporate names. The Shell Representative Office in Oman is an 
entirely distinct entity to Shell Oman Marketing Co., which is a listed company 
on the local stock exchange. Perhaps it has not helped that I am also the 
chairman of Shell Oman Marketing. But I think that more and more people now 
understand the difference; they know who I am and they know what I do and can 
make that distinction. <br>
<br>
As far as the general public is concerned, as more and more people come in 
contact with us through the various programmes we run, we get an opportunity to 
explain why we exist and what we do. I think there is a growing awareness of 
what the Shell Representative Office does and how it is different from Shell 
Oman Marketing. <br>
<br>
<b>But don’t you think more needs to be done to blot out this confusion?</b><br>
I don’t see it as something that has any negative connotation. Yes, it does 
sometimes create some confusion, but I see it purely as an administrative issue. 
I don’t see it as any real problem. After all, we both represent Shell. <br>
<br>
<b>How strong in your view is Brand Oman as an investment destination?</b><br>
I think it’s becoming very attractive. There are many traditional aspects of 
Oman, both of the country and its people, that make the Sultanate attractive. 
For example, I think Oman has a strong reputation for reliability; for being a 
partner that you can trust. Oman is also seen as a country that thinks very 
carefully about its decisions and for making prudent choices that are then seen 
through. This is best exemplified in the way the tourism sector is now beginning 
to take off. <br>
<br>
As a geologist by background, I am always most conscious of the natural 
advantages that Oman has from a environmental point of view – the fabulous 
scenery, the mountains, the coastlines, the deserts. But to me, another key 
attraction is the natural friendliness and hospitality of the Omani people, 
which will be a tremendous advantage for the future, especially for the 
development of tourism. <br>
<br>
I also think a strong work ethic is a strong differentiating factor for Oman. I 
often hear my Omani friends and colleagues bemoaning the fact that their 
children have little appreciation of what life was like in Oman 30 years ago, 
and take everything for granted. But we have found that the large majority of 
the young Omanis who participate in the schemes that we run have absolutely no 
doubts about the importance of hard work. They want employment, are prepared to 
work for it, and are very grateful for any opportunity that comes their way. <br>
<br>
<b>Now, coming to a few personal questions. What is a typical working day for 
you? </b><br>
I am not a particularly early starter. I start in the office at 7.30, whereas 
they start at 7.00 in PDO; I have an extra half hour in bed! Typically, I start 
the day by going through the diary with my secretary, working out when I have 
got to be where and how best to fit it all in. I spend a lot of time on the 
road, so we have to plan the day quite <br>
carefully to make sure that everything runs smoothly. <br>
<br>
I then check my emails, and the rest of my full day is taken up with meetings, 
either in my office or in the offices of one or other of the joint ventures. <br>
<br>
As I said, I spend a great amount of time talking to people outside Shell and 
the JVs, so no week goes past without several meetings with government 
officials, as well as people involved in the various social investment 
programmes. It all adds up to a fairly busy day, so it is usually only in the 
evening that I actually get to sit down and do anything ‘creative’. Then it is 
usually a case of rushing home for a quick shower before going out again to a 
function before crashing into bed and doing it all over again. And of course, 
like many businessmen with ‘Principals’ in Europe, I am often contacted during 
the weekend, which can make it hard to ‘switch off’! <br>
<br>
<b>
<img border="0" src="facetofacea2.jpg" width="150" height="205" align="left" hspace="5" vspace="2">What will you say of your experience in Oman so far? </b><br>
This is my third posting to Oman. I am now in my tenth year here and I have 
enjoyed every minute of it. When I was asked if I would like to come back for a 
third time – a rare scoop in Shell - I though about it long and hard… for all of 
two nanoseconds! I remember thinking that it was a privilege to be here 25 years 
ago and I feel the same now. Although the country has changed enormously since 
then, the good things about Oman don’t change. <br>
<br>
<b>What are your leisure engagements? How do you prefer to chill out?</b><br>
My main passion outside of work is sailing. Although I wouldn’t describe myself 
as a particularly gifted sailor, I just find it utterly, utterly relaxing! <br>
<br>
<b>How often do you go for sailing? </b><br>
Once a week, if I can. However, one of the main problems is that the wind here 
is a bit fickle; when I am in the office, the wind blows happily, while on 
Fridays, it never blows!<br>
<br>
My other hobby outside of work is acting. My wife and I are both active members 
of Muscat Amateur Theatre, which we first joined back in 1981 when we came here 
on my first posting. Some of the team members who were involved then are still 
involved now. It’s great fun, but hard work too. Successful amateur dramatics is 
something you’ve got to be committed to, otherwise you will not create something 
that will satisfy your audience, which is what it is all about. <br>
<br>
<b>Did you nurture acting ambitions in your youth?</b><br>
I used to act when I was at school and at Cambridge, but I never nurtured any 
ambition to act professionally. Mind you, the skills I have learned often come 
in useful in my business life; after all, acting is all about communication too!</font></p>
