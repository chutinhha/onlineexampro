‘Dream big to make big’<br>
<br>
</font></b><font face="Verdana" size="1">
<b>OER profiles Al-Qasim Al-Khalili, General Manager of Afras LLC, one of the 
leading suppliers of oilfield and petrochemical products in the Sultanate</b></font><b><font face="Verdana" size="1"><br>
<br>
</font></b><font face="Verdana" style="font-size: 14pt">
<img border="1" src="spe.jpg" width="150" height="201" align="left" hspace="5" vspace="2">“T</font><font face="Verdana" size="1">he 
moment we reached the office of Al-Qasim Al-Khalili, the General Manager (GM) of 
Afras LLC, the extraordinarily affable man in him makes us feel comfortable, 
almost immediately. And the scene shifts straight to the tete-a-tete. Al-Qasim 
has already cast a spell, courtesy his simplicity and forthrightness.<br>
<br>
Afras LLC, the organisation that Al-Qasim heads as its GM, is an Omani company. 
Established in the year 1988, it has gone on to become one of the most reputed 
suppliers of oilfield and petrochemical products in the Sultanate. <br>
<br>
Afras now represents numerous companies/manufacturers of repute from all over 
the world, and has partners/offices in the UAE, Kuwait, Iraq and Houston. 
Al-Qasim clearly has his task cut out as the captain of his ship. And for 
handling such a challenging task, he has the advantage of a good educational 
background to fall back upon. <br>
<br>
<b>Early years</b><br>
Al-Qasim has bachelor and masters in computer engineering from the Sultan Qaboos 
University. What made him choose computers? Did he have the inclination for it 
right since his school days? “Yes! Computers have been a passion for me since I 
was very young. By the time I joined the university, I had already decided to go 
for computer studies. I had two options: computer science or computer 
engineering. I chose the latter because it is more into hardware and networking, 
and more into electronics.” <br>
<br>
But Al-Qasim will not leave it at that. “I still plan to pursue higher studies 
and want to specialise in electronic trading and e-commerce.” So we ask him 
about the e-commerce scene in Oman. Is it making strides? “No, not yet! The 
e-concept is new, not only in Oman, but in the whole region here. Hopefully, 
this situation will change in Oman and the GCC in the coming few years,” he 
says, exuding optimism.<br>
<br>
We ask him about his first job. “Being a computer engineer, my first job was 
totally unrelated. I started with foreign currency trading, which, however, gave 
me a good exposure to the other side, that is foreign exchange business. A year 
later, I joined Afras. <br>
<br>
“When I joined Afras I was a little fresh and a little confused as well. I was 
first given the responsibility of the administration. A year-and-half after 
joining the company, I became the general manager. And by the grace of God, it 
is all going fine.”<br>
<br>
We ask him about his main challenges as the GM of Afras. “The first challenge 
was the communications part. It took me a little while to know the rules of the 
game. We started on a somewhat small scale but we have managed to get some of 
the best names on the list of our principals, in a variety of products in 
oilfield and equipment. <br>
<br>
“Some of the companies we represent are among the largest manufacturers of the 
concerned products worldwide. We also supply products to PDO. Though this gives 
us an edge, at the same time, we have to maintain a very high level of 
professionalism, and herein lies the challenge. We have had to raise our 
standards that much more. It has taken us lots of efforts, resources and time to 
reach here.” <br>
<br>
<b>Innovations, the other side</b><br>
At around the same time Al-Qasim joined Afras, he started his own IT company, 
called Innovations Computers and Telecom LLC. This was in the year 2000. He 
literally started his company from scratch.<br>
<br>
“Innovations is a company I started with just two employees,” he reveals. 
“Today, five years down the line, we have two divisions. One is the software 
development division where we have developed our own software targeted at the 
Omani market. The other division is the hardware maintenance where we have 
maintenance contracts with some of the ministries as well as some of the 
prestigious companies in Oman,” Al-Qasim informs. <br>
<br>
In fact, right since its inception, Innovations has provided a wide variety of 
industry-specific financial information software solutions to meet the business 
needs of various companies in different sectors. It has enabled an outstanding 
reputation for the development of leading edge, modifiable business management 
and customised software solutions. <br>
<br>
“The results at Innovations gives me the motivation to think bigger,” Al-Qasim 
asserts with aplomb. <br>
<br>
Finding time for running two businesses at the same timecan’t be an easy task. 
“True, it’s not easy. But the key here is that Afras is the business started by 
my father and is the main source of income for us. The priority, therefore, is 
Afras. On the other hand, since Innovations is the company I started from the 
scratch, it’s much easier for me to run it. Besides, I enjoy working in the IT 
field in my spare time. For me, IT is a passion so I don’t mind running 
Innovations. Things were pretty difficult initially, but now, after six years of 
perseverance, the vision is clearer,” he emphasises.<br>
<br>
And what motivates him to do such hard work? “I believe that if you have to 
reach somewhere, you have to dream big. Besides, I believe we (people in the 
Middle East) have all the potential but unfortunately we somehow lack 
self-belief. This needs to change now,” he says with the touch of a philosopher.
<br>
<br>
<b>
<img border="1" src="pers2.jpg" width="100" height="137" align="left" hspace="5" vspace="2">Overview 
of the market</b><br>
Al-Qasim is very positive about his company’s position in the market right now. 
“Oman being an oil-producing country, the main source of spending is on oil, 
especially in the last few years when the oil prices have gone northward. The 
market is good enough and there are lots of opportunities though the competition 
is quite stiff.<br>
<br>
As for the positioning of his company, he says, “We consider ourselves among the 
best companies giving services and products to PDO and other companies in the 
Sultanate. We believe we are in a very good position.” <br>
<br>
Al-Qasim is equally upbeat about the future. “We have very ambitious plans lined 
up for the future. We are updating technology in keeping with the demands of the 
time. We are getting in the frontline.” <br>
<br>
<b>Region’s scenario</b><br>
Where does he see the prospects of the IT industry in Oman and the region in the 
coming years, we ask him. “The GCC has a great potential to prosper and grow in 
the IT segment. This is the right time for Oman to go the whole hog as far as 
the IT sector is concerned otherwise we’ll miss the bus. The chances are 
unlimited. As for the technology, we are right there with everyone else, hand to 
hand.”<br>
<br>
And, compared to the other GCC countries, where does he see Oman placed as a 
destination for investment? “Oman is one of the best investment destinations in 
the region. It’s starting to unfold its potential now. We have lots of natural 
resources. We have maintained a very clean environment, which we have learnt 
from the mistakes of others. GCC nationals are coming to Oman. International 
entities are coming to Oman. Oman is becoming ‘the favourite’ GCC spot. Even if 
we look back into history, Oman was a favourite because of its location, 
geography and so many other factors.” <br>
<br>
Oman also has a large chunk of youth population. Isn’t it a great advantage for 
the country? We seek his opinion. “In the short run, it might be proving 
disadvantageous as well. This big chunk can be categorized as a working force in 
three segments: educated, non-educated and technical. But yes, in the long run, 
we’ll have lots of Omani hands working together to build this country. The only 
way this country will come up is by Omanis with the help of friends from outside 
but not the other way around,” he makes it clear. “We get the inspiration from 
the wisdom and vision of HM Sultan Qaboos Bin Said,” he adds.<br>
<br>
<b>Values and family life</b><br>
The value, that Al-Qasim says he cherishes most, is honesty. “Companies might 
perish and industries might go down; nothing is guaranteed, but what we strive 
for is satisfaction for the customer based on the value of honesty,” he states.
<br>
<br>
In the same vein, family is the nucleus around which he says his life revolves 
around.
Married, with three wonderful kids, his family means “everything in my life”. He 
adds: “My family gives me all the motivation to do whatever I am doing. They 
provide me the reason to go on in life. Their support is the key factor behind 
my success, if any. Things start at home.” He is absolutely clear about the 
paramount role of the family: “If things are right at home, it will be right 
everywhere. My philosophy is simple: Don’t bring home to work, don’t take work 
to home.” </font></p>

