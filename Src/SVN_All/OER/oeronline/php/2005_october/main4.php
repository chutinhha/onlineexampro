<br>
<b>Then</b> <br>
FW de Klerk was born in Johannesburg on March 18, 1936. He graduated in 
1958 from the Potchefstroom University with a BA and LLB (cum laude). He 
practiced law in Vereeniging, but in 1972, he declined the Chair of 
Administrative Law at Potchefstroom University to enter politics. That November, 
he became MP for Vereeniging. In 1978, he was appointed to the Cabinet, where 
over the next 11 years, he assumed several key cabinet portfolios. In March 
1982, he was elected Leader of the National Party in the Transvaal, then the 
national leader of the party in February 1989. In August 1982, after President P 
W Botha resigned, de Klerk became Acting State President. He was made State 
President on 20 September, 1989. During this period, he initiated the inclusive 
negotiations that led to the dismantling of “apartheid” and the adoption of 
South Africa’s first fully democratic constitution. De Klerk was appointed Joint 
Executive Deputy President after Nelson Mandela was made State President on 10 
May, 1994. Despite his party withdrawing from Government in June 1996, De Klerk 
continued on as Leader of the Official Opposition until his retirement from 
party politics in 1997.<br>
<br>
<b>Now</b><br>
FW de Klerk works through the F W de Klerk Foundation, to promote peace in 
multi-communal societies. He acts as&nbsp; an elder statesman in conferences 
worldwide, discussing the future of Africa and South Africa, and the challenges 
confronting the world during the new millennium. His Global Leadership 
Foundation, headquartered in London, has been created with other key leaders to 
assist in the promotion of peace, democracy and development in divided 
communities. De Klerk is also the Honorary Chairman of the Prague Society for 
International Co-operation; a member of the Assembly of the Parliament of 
Cultures in Istanbul and a member of Forum 2000. He serves on the advisory 
boards of the Peres Centre for Peace in Israel and the Global Panel in Germany.
<br>
