            path of development and prosperity. The woman power is no longer 
            restricted to holding jobs such as schoolteachers, secretaries or 
            nurses. Today, they are holding positions of power in many 
            industries whether you take banking, finance, media, retail, 
            hospitality, medical, real estate etc. <br>
            <br>
            In this special cover story, OER focuses on the emerging women 
            managers in the Sultanate who have done exceedingly well in their 
            careers and have the fire to reach the top. They are a new breed of 
            women managers who are well educated, confident, ambitious, and have 
            the talent to join the ranks of top decision makers like GMs, CEOs 
            or MDs. Currently, there are very few women in senior positions, 
            leading companies. But when you look at this new breed of talented 
            middle level managers, you can be assured that the scenario is going 
            to change dramatically with more women CEOs and MDs set to emerge.
            <br>
            <br>
            Through their determination and performance, they have managed to 
            break the glass ceiling, created by attitudinal and organizational 
            prejudices, barring women from holding the seats of authority. They 
            have consciously taken up the non-traditional career paths by 
            getting into professional and line management jobs for advancement 
            within enterprises. They have avoided joining non-strategic sectors 
            and in personnel and administrative positions which block their 
            progress to the top.<br>
            <br>
            Shattering the myth that career oriented women fail to do justice 
            with their family responsibilities, most of our protagonists have 
            managed their professional and personal lives with equal aplomb. A 
            closer look at the profiles of these women proves that like a woman 
            is behind the success of a man, similarly a supporting man is behind 
            the success of a woman. Most of these successful managers have good 
            support of their families who have encouraged and given them the 
            freedom to study and work as per their liking.<br>
            <br>
            A majority of these women belong to families with a progressive 
            outlook. They were encouraged to pursue higher studies and sent 
            abroad to renowned educational institutions. Even after marriage, 
            they continued adding new skill sets by studying further or taking 
            up new projects to fullfil their professional goals. In the more 
            advanced and open markets, women are coming in forefront and leading 
            number of large corporation with great success inspiring million of 
            other women to excel in professional life. Similarly, these are the 
            women who are going to inspire thousands more and this enlightenment 
            will go a long way in realising the Sultanate’s vision 2020. With 
            these women, OER looks at the future today!<br>
