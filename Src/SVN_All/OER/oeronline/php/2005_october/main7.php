<img border="0" src="carr1.jpg" width="400" height="233"></p>
<p align="justify" dir="ltr">
<font face="Verdana" style="font-size: 12pt; font-weight:700">
C</font><font face="Verdana" size="1">omfort, design and dynamism are the three 
main factors that catch your attention after a quick ride in the new Toyota 
Innova. The Suhail Bahwan Group introduced the much-awaited Innova, which has 
already been launched in other markets, last month to Oman. <br>
<br>
Toyota does not disappoint with its new entrant in the MPVs section. And when it 
has got the Toyota seal you can be sure that what you get to see is what you 
get. <br>
<br>
The automobile giant’s expertise in the business of knowing what the consumer 
wants is clearly visible in the Toyota Innova. The apt vehicle for a family with 
many needs, the Innova is definitely going to strike a chord with the consumers 
in Oman. The reasons, besides the three mentioned above, being its multi-purpose 
factor, all-round safety and global product quality. <br>
<br>
<b>Exteriors</b><br>
Measuring 4555 mm in length, the Innova may not exactly grab your attention for 
the exterior styling but when it comes to size, you just cannot ignore the new 
vehicle from Toyota. The Innova is big and you just can’t help noticing the 
MPV’s size. As for the exterior styling, the Innova does not scream out for 
attention but portrays a tough and stable outlook. Uniformity is maintained when 
it comes to the colour as the bumpers are the same shade as the body. <br>
<br>
The front grille is aesthetically designed and the chrome accents on the vehicle 
that include door handles, outer mirror and rear license plate garnish add value 
to the otherwise simple design of the MPV. <br>
One design aspect that caught our attention was the upswept large headlamps that 
curve up to the fenders and give the Innova a distinct look. <br>
<br>
The wheels, surprisingly, look quite small on the big MPV and could make you 
wonder if they are adequate enough to carry all it has to, passengers and 
luggage included. <br>
<br>
<b>Interiors</b><br>
Going by the sheer size of the Innova, you don’t need a second person to tell 
you that spaciousness is a key factor in the MPV. Interior space is huge and 
almost luxurious. The company says you can fit in eight people, though we would 
prefer seven for a really comfy rid. After all, a big car should ideally 
translate to enhanced traveling comfort. The leg space is impressive, and the 
second and third row of seats can be folded down in case you are in a serious 
luggage-moving situation. <br>
<br>
Another interesting feature is the quality of the interior décor. Though not 
positioned in the higher end category, the Innova’s interior designs are quite 
eye-catching and even have luxury feel. The front seats are quite large with 
seat- and steering wheel-height adjustment facility. Up front in the driver’s 
seat, you sit high and feel quite like a king on a throne especially since what 
you are driving looms like a giant on the roads. <br>
<br>
Cup holders, storage pockets and cubbyholes are aplenty in the Innova and no 
one’s going to complain about storage space. The dual air conditioner vents are 
quite appealing as this ensures that all the passengers, including those in the 
rear seat get the draft.<br>
<br>
Toyota has tried to add a mod look to the MPV wherever possible. One such touch 
is the multi-information display unit on the centre console. <br>
<br>
Yet another feature that would especially go down with music lovers is the CD 
player in the Innova, which comes with six speakers. Pump up the volume all the 
way!<br>
<br>
<b>Performance and safety</b><br>
Once you are behind the wheels of the Innova you feel like you want to take on 
the world. Guess that’s a feeling you just cannot ignore when you are handling a 
big vehicle. For the time being, we decided to just drive around town and get a 
feel of the vehicle’s performance. <br>
<br>
The roads of Muscat seemed to welcome the Innova for driving the MPV felt no 
different than other vehicles of the same caliber. And it moved with an agility 
that would surprise you for the sheer fact of its size. The stability of the 
Innova bears the unmistakable mark of all Toyota models; one that instills loads 
of confidence in the driver. <br>
<br>
The 2.7 litre 4-cylinder engine impresses with its superb road dynamics, be it 
on the highway or the traffic infested Qurum-Al Khuwair route at 2.00 pm. To 
begin with, sharp turns and maneuvers might seem a tad too difficult for the MPV 
taking into consideration its size. <br>
<br>
But that’s where Toyota proves that it still is a leader when technology is 
concerned. The Innova handles sharp turns quite admirably, an advantage of it 
being a rear-wheel driven vehicle. Though we never got to test the fuel 
efficiency of the Innova, the manufacturers ensure that it is high with better 
mileage quality. <br>
<br>
The Innova comes equipped with anti-lock braking system. Toyota has gone a step 
ahead with the installation of shatterproof windshield glass. Innova's also 
boasts of a cabin with front and rear crumple zones equipped with side impact 
beams that ensure occupant safety in case of collisions. <br>
<br>
On the whole, the Toyota Innova is a clear winner in the MPV section. Its 
contemporary design, near-luxe interior feel and admirable drive comfort make 
the Innova a strong contender even in a passenger cars segment. N</font></p>
