<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt">
<img border="0" src="mark1.jpg" width="250" height="226" align="right">L</font><font face="Verdana" size="1">et’s 
be clear from the kick-off. Advertising depends on the quality of ideas that 
stand behind the product or service. Everyone recognises an idea when they see 
one, especially if it’s big. In fact, big ideas drive companies and brands to 
extraordinary levels of achievement. But how many big ideas have you seen 
recently? The way I see it, an idea that doesn’t make a difference isn’t an idea 
at all. The bigger the difference, the bigger the idea. I suppose you could say 
that an idea is only as big as the difference it makes. In this regard, are the 
ads on Oman’s highway billboards and those published in our magazines and 
newspapers really making a difference? I’m not entirely convinced.<br>
<br>
Advertising is a seriously complex business and an ever-changing science and 
much has been written on the subject. In fact, you’re probably utterly confused 
about what does and doesn’t work. Indeed, judging from the quality of 
advertising we’re often exposed to in Oman, it’s apparent that some are more 
confused than others. Everything discussed in this two part article (as in all 
my OER columns) has one goal – to help you make more money – substantially more, 
with your advertising and marketing. So, shall we get started? <br>
<br>
<b>No Guesswork</b><br>
Any ad campaign can be broken down in terms of cost per thousand and if you’re 
looking for a direct response, it should be monitored according to cost per 
response. Advertising cost per thousand includes cost of origination (design), 
production (printing) and media (such as Oman FM, The Oman Daily Observer, 
display advert, list procurement and postal fulfilment). Generally, you’ll pay a 
higher cost per thousand for better targeted methods, but in return you should 
expect a higher response rate, so the cost per response can be lower than 
cheaper methods. <br>
<br>
Choose advertising and publicity methods that suit your target markets. 
Organisations selling advertising are able to provide a lot of information about 
their readership/audience and you can look at other advertisers repeatedly using 
various media. Ask yourself - are they targeting the same audience as me? Even 
very sophisticated advertisers only commit to major programs after accumulating 
response data from pilots and previous campaigns. They avoid guesswork and so 
should you. Any large scale activity must first be tested and the response 
measured for quantity and profile.<br>
<br>
<b>Build Awareness &amp; Generate Response</b><br>
What are you tying to achieve, create awareness or generate a direct response? 
Effective marketing generally demands that each is employed, but on a limited 
budget you may be restricted to concentrating on one or the other, so think 
carefully about what will help most. Different media and methods are better 
suited to one or the other. Direct Mail is good at generating a direct response, 
as are magazine and newspaper adverts and inserts. Posters, Oman TV, FM and 
press editorial are all much better at creating awareness and building 
credibility.<br>
<br>
<b>Easy Language</b><br>
In all your advertising material make sure you see and hear things form your 
customers' perspective. As a knowledgeable copy editor there’s always a tendency 
to write copy and present information from a technical and informed standpoint. 
Remember, your customer is ordinary Joe Blow and you need to spell things out to 
him, using clear simple language. Don’t fall into the trap of thinking that 
complicated language helps build an image of professionalism and intelligence – 
people will just switch off. The mark of true advertising intelligence is the 
ability to convey complex issues in a manner that’s interesting and easy for Joe 
Blow to digest. Thomas Jefferson suggested: &quot;The most valuable of all talents is 
that of never using two words when one will do.&quot;<br>
<br>
<b>Meaningful Customer Benefits</b><br>
Focus your message on a few key strengths of your business - for example, the 
distinct service you offer - you must now express these in terms of benefits to 
your customers. What does it all mean to them? Give them something to relate to, 
so that you don't just talk about what you do - talk about what it means to your 
customers. How will it make their lives easier, better, more comfortable or less 
stressful?<br>
<br>
<b>Cost it</b><br>
Because advertising is such a complex science the only real way to be sure that 
something will work before you try it is to refer to previous indicators. Start 
measuring the effectiveness of your advertising from the very beginning. Keep 
detailed records of what you did, when, to whom, for how much and what resulted.
<br>
<br>
Admittedly, the results of certain advertising are difficult to measure, 
particularly where no direct response is sought, when follow-up sample surveys 
are the only way to gauge performance. However, if you don't measure and record 
results you'll be missing the opportunity to make your task much easier next 
time around. If your advertising doesn’t produce a gross profit in excess of its 
cost you need to know why. And as a minimum, you certainly need to know whether 
it does or doesn't.<br>
<br>
<b>AIDA</b><br>
The Attention part is the banner or headline that makes an impressive benefit 
promise. Interest builds information in an interesting way, usually meaning that 
this must relate closely to the way the reader thinks about the issues 
concerned. If you seek a response you must move then to create Desire, which 
relates benefits to the reader so that they will want them. Finally, you must 
prompt an Action, which may be to call a telephone number, visit a Website or 
complete and return a reply coupon. Advertising that doesn’t prompt action is a 
wasted opportunity, understood?<br>
<br>
<b>Your Main Message</b><br>
Don’t be tempted to devote 50% of the space to a striking picture or a quote 
from your Chairman. The biggest part of the advert must be your main benefit 
statement. This is the part that entices the reader to read on.<br>
<br>
<b>Impressive Benefit</b><br>
Research proves that where responses are required, the best advertising is that 
which offers an impressive, relevant benefit to the reader. This promise should 
ideally contain the business brand name, take no longer to read than is normal 
for the media (direct mail is about 4 - 8 seconds, or about 15 words) and be 
clearly the most striking part of the ad. This point cannot be stressed enough; 
you must keep it quick, simple and to the point. And the trend is for ever 
quicker points: Copy is getting shorter and a major factor behind this is that 
people these days suffer from acute shortages of both time and attention. 
Younger generations are extremely visually literate - they’ve been brought up on 
computer games, so they don’t deal with a lot of polished copy. Think about the 
vocabulary and language you use - know your target audience: a simple test is to 
avoid any words or grammar that would not be found in the newspaper that the 
target group would read.<br>
<br>
<b>Quick &amp; Easy</b><br>
Use a clear layout, fonts and language. Don’t distract the reader from the text 
by overlaying images or using fancy fonts. Use simple language, avoid 
complicated words and keep enough space around the text to attract attention to 
it. Use simple traditional typestyles: serif fonts are quicker to read than sans 
serif. Use 10, 11 or 12 point-size for the main text; smaller or larger are 
actually more difficult to read and therefore less likely to be read. Look at 
newspapers and magazines which are almost always serif fonts of 10 to 12 point 
size.<br>
<br>
Avoid Clutter<br>
Make it easy to read. For the same reason avoid italics, shadows, light colours 
reversed out of dark, weird and wonderful colours. None of these improve 
readability, they all reduce it. Use simple black (or dark coloured) text on a 
white (or light coloured) background. Involve the reader in your writing style - 
use the second person: you, your, and yours. Refer to the reader as 'you' and 
use you', 'your' and 'yours' in the description of what your business does for 
the customer to get them visualising their own personal involvement. Describe 
the service as it affects them in a way that they will easily relate to it.<br>
<br>
<b>Say Something New</b><br>
People respond better and are more easily attracted to a concept that’s new or 
original. If they've heard or seen it all before they’ll take no notice. People 
must believe there's something in it for them right from the start. Develop a 
proposition that’s special or unique and emphasise this.<br>
<br>
<b>Be Different</b><br>
You must emphasise what makes your product/service special. Unless your code of 
practice prevents you from claiming superiority over your competitors, you 
should put as much emphasis as you can behind your USP (unique selling point), 
and either imply or state directly that you are the only company to offer these 
things. Your proposition or offer must be credible.</font></p>

