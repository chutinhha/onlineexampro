            <p align="left"><br>
			<b><font face="Verdana" size="2">Annual Budget 2006</font><font face="Verdana" size="5"><br>
			</font><font face="Verdana" style="font-size: 16pt">Another year of 
			steady, but conformist fiscal planning</font></b></p>
            <p align="justify"><font face="Verdana" size="3"><b>O</b></font><font face="Verdana" size="1">man 
			ended 2005 on a high note. Instead of the planned deficit of RO 540 
			million, the Sultanate closed fiscal 2005 with a surplus of RO 1,479 
			million - despite additional burden of RO 331 million on the 
			expenditure side. The oil exports generated additional revenue of RO 
			2,350 million above the budget estimates. In the beginning of 2005, 
			the oil revenues were calculated on an estimated oil price of US$23 
			per barrel but the actual price realised was US$48.73 per barrel. 
			The unexpected higher revenue led to an impressive GDP growth rate 
			of 21.7 per cent (2004: 12.5 per cent). The achievement is more 
			laudable as the year’s best performing economies like China and 
			India clocked growth rate between 7 and 10 per cent. <br>
			<br>
			Assessing the public finance performance in 2005, Ahmed bin 
			Abdulnabi Macki, Minister of National Economy and Deputy Chairman of 
			Financial Affairs and Energy Resources Council said: “The positive 
			performance covered the approved budget deficit and financed the 
			additional expenditure approved during the year. It also reduced the 
			public debt and has increased the financial reserves.” <br>
			<br>
			With the comfort of last year’s surplus of RO1,470 million and oil 
			prices continuing to be above US$60 per barrel, economists had 
			expected Oman to shift gears and announce some major investment 
			initiatives in the annual budget for 2006. But realising the 
			volatility in the international oil market and Sultanate’s much 
			larger population and lower oil reserves (compared to many of its 
			GCC neighbours), Oman has continued to be steady-but-conservative in 
			its fiscal planning. Instead of committing large sums from the 
			reserves in long-term multi billion dollar projects, Oman’s vision 
			is to continue treading on the path of sustainable growth. <br>
			<br>
			The Plan 2006 lays major emphasis on the socio-economic development. 
			With 68 per cent of Oman’s population up to or below the age of 24 
			years, the education sector has been given a major thrust: it has 
			been increased by 17 per cent over the last year. Bulk (close to 38 
			per cent) of the civil ministries’ expenditure is allocated to the 
			education sector. <br>
			Keeping in mind the welfare of the population, the health sector 
			expenditure has also been increased by 13 per cent over the last 
			year. Similarly, RO 29.8 million has been allocated to the social 
			security, a 21 per cent increase over the last year’s figure. The 
			oil and gas production expenditure has been raised by 39 per cent to 
			RO 196 million. Macki explained that such additional investments (in 
			oil &amp; gas) are aimed at maintaining the production levels and 
			continuance of the exploration and drilling operations and 
			conduction of studies. The defence and security expenditure has been 
			increased by 8.5 per cent to RO 1245 million. <br>
			<br>
			The budget may raise concerns in the corporate sector. The Plan 
			projects corporate tax revenue of RO 72 million, an increase of 31 
			per cent over RO 55 million in 2005. The fee on non-Omani labour 
			licenses is expected to be RO 38 million. The customs duty revenue 
			is budgeted at RO 75 million, an increase of 25 per cent over RO 60 
			million in 2005.<br>
			<br>
			Out of the total expected revenue of RO 3,587 million, oil is 
			expected to contribute 70 per cent (RO 2,519 million). The 
			projection is based on an estimated oil price of US$32 per barrel. 
			The oil revenue in 2006 is expected to be much higher than the 
			government’s projection if we go by the forecasts of international 
			economists. <br>
			<br>
			“Oil prices that shot up by 40 per cent in 2005 are approaching 
			US$70 per barrel again. Prices are expected to average US$50-60 per 
			barrel in 2006 despite improvement in oil supply and demand 
			moderating,” predicted Sarah Hewin, the London-based senior 
			economist with American Express Bank, while talking about global 
			economic trends in Muscat recently. Even if we take US$50 as the 
			average price, Oman’s oil revenue is going to jump by 56 per cent. 
			The additional RO1,410 million, as a result of the increase, will 
			not only wipe out the projected deficit of RO650 million but also 
			generate surplus of RO760 million. <br>
			<br>
			Even though the government has been conservative in its estimation 
			of GDP in 2006, as the trends suggest, Oman is again going to end 
			the year with a double-digit growth rate. And another booming year 
			may make Oman further open its reserves for infrastructure 
			development and non-oil diversification activities. by Akshay 
			Bhatnagar</font></p>
