<p align="justify" dir="ltr">
<font face="Verdana" size="3">
<b>I</b></font><font face="Verdana" size="1">believe that a good educational 
background and the valuable experience one earns over the years, both complement 
each other. As Oscar Wilde once said, &quot;Teaching is an admirable thing, but it is 
well to remember from time to time that nothing that is worth knowing can be 
taught.&quot; I started my career with an international bank but took a break on the 
&quot;Trailing Spouse and Mommy Track&quot; when my children were born in Montreal. Prior 
to joining British Airways in 1998, I used to run my own business, which I sold 
off before taking up the new assignment. <br>
<br>
I have always used different rules of engagement. Leadership, I believe, is not 
a solo act. The Lone Ranger, according to me, is a myth. I see leadership as a 
chorus of different voices ultimately singing in unison. The developments in air 
ravel since 2001 has set a new agenda for industry leaders. We live in a period 
of profound transition. My role as the British Airways Country Manager is to 
pilot the team as well as individual and commercial performance out here; to 
introduce and mediate changes in Oman and Yemen and to ensure that when changes 
occur, they do not look or feel like upheavals, but are communicated and 
understood properly. This is how and why people feel good and confident about 
the way we conduct our business. </font></p>
<p align="justify" dir="ltr">
<font face="Verdana" size="1">
Leading a team simply means tapping into the enthusiasm, creativity and 
intelligence of those you work with. It also means having fun. No matter how 
well you think you know your job, it'll become dull unless you relish it. And 
this is where some of our best initiatives and business plans have come from!
<br>
<br>
Each one of us can come up with good ideas and inspire others for the same, and 
think wider than one normally does. Business is, after all, a series of 
paradoxes. And intelligent people love paradoxes. It may be puzzling at times 
and stretching the mind, but the truth is that fresh insights often come out of 
the confusion. It is important to do some thinking on work in a free-format 
where people have the confidence that they won't be laughed at. <br>
What is creativity after all? It’s about linking two seemingly unrelated things! 
How does one get inspired? Play music. Break bread with a colleague. Listen to 
someone else's story. Share opinions. Go to a new destination together. Laugh. 
Resist the tyranny and bullying of self-important drones. <br>
<b><br>
Alignment keeps business focused</b><br>
There are two dimensions of leadership: performance and values. I don't think 
you can have one without the other. Trust, integrity and fairness do matter and 
they are crucial to the bottom-line. Success in your career today generally 
requires a fit between your personal style and values on one hand and the 
company culture on the other. Alignment and adaptability are both crucial for 
long-term success. With clear-cut objectives and a commonly shared vision, 
alignment keeps a business focused. But it can also keep a company engrossed in 
habits of the past, leading to red tape and painfully slow decision-making. This 
is where adaptability comes in. Organizational adaptability is the 
institutionalized ability to detect and cope with changes in the external 
environment. Incremental adaptability is also important, as it involves the 
constant fine-tuning of knowledge, products and services. <br>
<b><br>
Courage is the vision </b><br>
Performance, performance, and performance. That is the bottom-line. Balancing 
the speed and desire for change with the practical realities of developing 
people, technology, processes, regulation, governance and relationships with 
customers, industry partners, stakeholders and suppliers is an ongoing 
challenge. <br>
<br>
To be a good manager, you have to have the corporate vision with a strategy that 
takes into account the people you have and the place you are posted at. You must 
have the courage to do what you think is right in order to translate the 
company's vision into reality. And you have to have a reasonable track record of 
success. <br>
– As told to Akshay Bhatnagar</font></p>

