<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>In an exclusive 
write-up for OER, H.E. Dr. Mohammed bin Hamed bin Seif Al Rumhy, the Minister of 
Oil and Gas, spells out the expectations of the oil and gas industry from the 
current year. The challenge for Oman, according to him, will be to introduce the 
latest available technology to maximise production and reduce the rising costs</b><br>
<br>
</font><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
demand for oil, globally, should grow at a robust pace over the next five years 
as developing countries such as China and India increase their imports. While 
global energy consumption will increase, oil consumption is expected to grow at 
1.4 per cent per annum and gas is expected to grow at 1.8 per cent per year. Oil 
and gas will account for 60 per cent of the world’s energy needs. It is 
estimated that most of the growth will come from the developing economies. The 
rapid growth in demand for oil may well test the global supply capability. To 
satisfy the ever-increasing thirst for crude, OPEC will have to produce more.<br>
<br>
As for the price projection, the consensus is around $40-50 per barrel in the 
short term, with a declining dollar value. Shifting oil payments to a mixture of 
currencies such as dollar/euro/yen is unlikely in the current climate.<br>
<br>
<b>Oman’s petroleum industry:</b><br>
Oman has 50 billion barrels of Oil Initially In Place (STOIIP). As of 31 
December 2005, 7.3 billion barrels have been produced, representing 14.5 per 
cent of STOIIP. As it is evident from the STOIIP, Oman has a long and bright 
future with regard to oil production. While this does not mean that the entire 
oil can be produced under the currently known technology, it is possible to 
produce at least 30 per cent of the 50 billion barrels. <br>
<br>
Oil and gas are limited and deplete-able resources. The best policy for Oman -- 
if we can adopt a long-term, flexible policy for production -- is to produce a 
suitable rate for an extended period of time with a view to serving the future 
generations. In 2006, the companies will strive to achieve an average oil 
production of around 745,000 barrel per day (BPD). And oil price is anticipated 
between $40 and $50 per barrel, which is more than the adopted price in the 2006 
budget. <br>
<br>
The challenge for Oman will be to introduce the latest available technology to 
maximise production and reduce costs, which have been rising. The Ministry of 
Oil and Gas (MOG) has taken steps to ensure that cutting edge technology, from 
whatever source, will be made available to Petroleum Development Oman (PDO), 
under new arrangements agreed with our partners: Shell, Total and Partex. This 
breakthrough arrangement will provide PDO with the flexibility to source 
technology from all over the world at competitive prices. <br>
<br>
As for gas, this vital resource, combined with oil, forms the backbone of the 
Omani economy. The emergence of gas since 2000 as the second largest contributor 
of income to the Treasury has meant that gas has made significant strides as a 
resource provider, not only as a source of revenue, but also as a driving force 
for the industry, especially after Qalhat Liquefied Natural Gas Company (QLNG) 
started production and export of liquefied natural gas (LNG) from the third 
train and the industry-related products based on gas in Sohar and other 
industrial parks. <br>
<br>
The MOG in its role as custodian of hydrocarbon resources is actively pursuing 
ways and means to shore up its gas reserves with a view to facing the high and 
increasing demand. From 2006, the MOG has doubled its budget for gas 
exploration.<br>
<br>
The other prospective producers trying to achieve gas production target are 
Occidental and Petroleum Authority of Thailand Exploration (PTTEP) Company. They 
are trying to develop and improve the up-stream gas facilities in the fields to 
increase the production capacity to supply gas to the current and the new 
projects.<br>
<br>
In 2006, gas supply and demand will increase rapidly when QLNG, Sohar Refinery, 
Oman India Fertilizer in Sur and some industrial projects in Sohar such as 
Methanol, Sohar Fertlizer and others work in full capacity.</font></p>

