</font><b><font face="Verdana" size="5">Blast-ing out monotony</font></b><font face="Verdana" size="1"><br>
<b><br>
Mehre Alam catches up with Minoo Saher, the CEO of Mustafa Sultan Enterprises, 
to dig deep into his favourite pastime</b><br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="passtime.jpg" align="right" hspace="5" vspace="2">E</b></font><font face="Verdana" size="1">ver 
heard of a company CEO holding a golf stick right in the middle of his office 
room? And someone who’s not just holding the stick, but putting some balls too! 
Meet Minoo Saher, the Chief Executive Officer of Mustafa Sultan Enterprises. If 
you think this CEO is having a ball, hold on! He is just unwinding - for a few 
casual moments - to spool back his energies for some more brainstorming sessions 
in office.<br>
<br>
Pastime, according to Minoo, is kind of catharsis. “There are these pressures of 
daily life linked with the performance of one’s job. It’s very important to have 
some kind of structured activity. You should be able to indulge in it in a 
manner that makes you happy doing and at a time when you feel it’s all yours,” 
he adds. <br>
<br>
When we catch up with him at the Al Maha Golf Course in Muscat on a bright day, 
Minoo is all decked up for a brush with golf. And the golf buff looks deadpan 
serious too! Probably more straight-faced than what he may look like in the 
precincts of his office. Pastime is a serious business for him. “I am very 
serious about my time. I am consciously aware that time is a finite resource,” 
Minoo, basking in his golfing attire, tells us. <br>
<br>
How and when did he pick up this passion for golf? “One of my friends in Muscat, 
an avid golfer himself, brought me to the golf course. He kept on goading me to 
pick up the stick. That’s how I started, went for a few practice sessions and 
played a few games,” Minoo reveals. <br>
<br>
How good a golfer he is? “ Well, I am really terrible,” avers the modest golfer. 
“In fact I would like to continue to believe that I am terrible because that is 
the only one way of getting a little better. I do remember of my initial days 
when I had just started playing golf. After the first 10-15 days, you start 
feeling you’re getting the swing and you feel you are now able to connect the 
ball a little better. So when you are driving to the course, you are thinking 
you are going to have a great game today. But it turns out to be really lousy 
game. You thought you had improved but you actually start flying the carpet up 
and missing the ball altogether. Even good golfers often feel that they are not 
up to the mark regardless of the handicaps they are working themselves up to. 
The day you feel you are very good, you will stop improving!”<br>
<br>
For a very busy CEO, how difficult it is to eke out time for a tee-off at the 
course? “I think it’s not difficult. It’s a mindset. It’s a matter of 
discipline. It’s also not a function of how busy you are, but how well you 
manage your time,” Minoo explains. <br>
<br>
Minoo, who seems to be a stickler, literally, is very conscious of his time. 
“Every time I see any time being wasted I actually feel quite concerned,” he 
tells us. “Time is a very important commodity and any waste of it is totally 
unacceptable to me. Sometimes if an hour goes waste, I am consciously aware this 
one hour will never come back.” And what about the hindrances to his golfing 
pastime? “I do believe that – though it may sound like an excuse – the absence 
of a green course in Muscat has a lot to do with my lack of proper interest. The 
day I have a green course to go to in Muscat, I’ll probably spend more time on 
the green courses.” As for the positives, one event that Minoo is eagerly 
looking forward to is the OER CEO Golf 2006. “Last time, I was really touched by 
the spirit of the event. The whole atmosphere was so vibrant, so picnic-type.”
