    <font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1">n 
    2000, Qatar became the first country in the region to win the right to host 
    the Asian Games. Doubts were raised on how Qatar, a relatively smaller 
    country with an area of 11,437 sq kms, is going to host the 15th Asian Games 
    in its capital city Doha in December 2006! Though the real test is still 10 
    months away, Qatar is all set to deliver on its promise. Over US$2.8 billion 
    has been invested to create the infrastructure for the games. Qatar is 
    gearing up for the event with over 30 sports facilities, including 14-multi 
    purpose stadiums and world’s largest sports dome, swank shopping malls, 
    apartment complexes, luxu ry hotels and much improved traffic infrastructure 
    in place.<br>
    <br>
    The question is why Qatar decided to bid for hosting such a large-scale 
    event ahead of rest of the West Asia (Iran being the only exception as it 
    hosted Asian Games in 1974)? Perhaps the answer lies in the fact that 
    organizing a large-scale sports event creates a multiplier effect on the 
    socio-economic and cultural horizon of the host country. <br>
    <br>
    The economic impact, in this case, means “the net economic change in a host 
    community that results from spending attributed to a sports event or 
    facility”, according to Turco &amp; Kelsey. The economic impact is arguably one 
    of the most important indicators of the success of a major sporting event, 
    and often one of the main incentives for intending to host the event 
    initially. In terms of direct revenues, Busan Asian Games in 2002 earned an 
    estimated US$120 million. The 2010 Asian Games to be hosted by China is 
    expected to generate a profit of US$75 million. But the host country looks 
    for much wider economic gains in the long term, instead of the short-term 
    profits only. <br>
    <br>
    Mega sports events promote economic activity as a result of the 
    opportunities created to build the requisite infrastructure. More than US$2 
    billion were invested in Olympic-related projects in Atlanta that hosted the 
    1996 Olympic Games between 1990 and 1996. Over 580,000 new jobs were 
    created, resulting in a rapid economic development of the region. In 2000, 
    the year in which Qatar was awarded the hosting rights of Asian Games, its 
    GDP was US$17.76 billion (at current prices). But in 2006, Qatar’s GDP is 
    expected to race to US$45.167 (at current prices). Among all the GCC 
    countries, Qatar’s GDP growth rate is going to be the highest in the period 
    2000-2006. Though there might be other factors at work, but the catalyst 
    role played by the Asian Games in the growth of Qatar cannot be ignored. <br>
    <br>
    The economic benefits of urban regeneration and global media exposure are 
    carried post-event. Qatar too is looking at the games to further strengthen 
    its tourism potential. The confidence stems from the fact that the new Doha 
    International Airport is expected to be operational by 2009 and Qatar 
    Airways is expanding its aircraft fleet from 43 to 123 by 2015. Qatar has 
    also sketched a plan to transform some of its newly constructed 
    infrastructure facilities into a multi-specialty health and education 
    center. <br>
    <br>
    Besides economic benefits, the large-scale sporting events have a 
    long-lasting social impact on the community, which hosts the event. It 
    alters the way in which people live, work, play, relate to one another, 
    organize to meet their needs, and generally cope as members of the society. 
    The events also lead to cultural impacts involving changes to the norms, 
    values, and beliefs that guide and rationalize the cognition of the 
    individuals and the society at large. In a way, the event rejuvenates and 
    binds the resources of the country to play a larger role in the global 
    economy than ever before. Sports are no longer about the physical 
    performance of individuals. It is a magic potion for the nation’s health as 
    well.</font></td>
