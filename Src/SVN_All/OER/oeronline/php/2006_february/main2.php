		Qatar Airways is privileged to be in the position it is today. Its 
		success in the short period of its existence is a reflection of the 
		sheer hard work and dedication of its employees. Receiving awards is 
		recognition of the airline’s tremendous achievements. To receive any 
		award is an honour, but the greatest achievement we aspire towards would 
		have to be being named ‘Airline of the Year’ by our industry colleagues 
		and the travelling public.</font></p>
