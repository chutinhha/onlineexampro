		hub-and-spoke model of feeding passengers in and out of our Doha hub. 
		This model will continue to be an integral part of our global strategy; 
		hence our passenger mix will continue to be regional and global. The 
		Middle East is a key regional and global hub – and airlines in this 
		region have always enjoyed traffic growth despite growing competition 
		from more direct carriers around the world.</font></td>
