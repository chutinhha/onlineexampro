<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Part of the 
intended 80 new aircraft will be 20 Boeing 777s, for the first time since the 
re-launch in 1997 mixing up your all-Airbus fleet. What made you decide to 
complement your existing fleet with the Boeing aircraft? What impact will the 
decision have on your maintenance facilities? </b><br>
The decision to add Boeing aircraft to our all-Airbus fleet was taken after very 
extensive analysis. While we continue to expand our Airbus fleet, it was felt 
that the Boeing 777 had significant advantages in serving long-haul markets and 
the aircraft would become our standard wide-body plane. <br>
<br>
Having aircraft from two manufacturers simply widens our options. With regards 
to maintenance facilities, we will have in place facilities to handle the 
maintenance of our Boeing aircraft. Adding Boeing to our fleet will have no 
significant impact on our maintenance facilities.<br>
<br>
<b>The New Doha International Airport has not been in the news for a while. How is 
the work going? Are you on schedule to open phases one and two in 2009? </b> <br>
The New Doha International Airport is well on schedule to open for business in 
2009. The environmental clean-up project of the site has been completed. This 
included the completion of the extensive waste removal on the site. Actual<b><img border="0" src="face2face1.jpg" width="300" height="117" align="right" hspace="6" vspace="6"></b> 
reclamation work on the site began a year ago and significant progress has taken 
place. Foundation piling work for the construction of the building structures is 
also taking shape.<br>
<br>
<b>What is in store for 2006? Are you planning to add further destinations, or will 
you concentrate on adding frequencies?</b><br>
2006 will be as exciting as previous years. We will be adding more routes to our 
global network, which currently stands at 69 destinations. Hong Kong will be 
launched at the end of March, and we have further destinations planned over the 
next 12 months, which will be announced in due course. Apart from new routes, 
2006 will be a year of increasing frequency on existing services. We plan to 
offer greater choice and more travel options to our passengers. Additional 
frequencies will be introduced on European, African, Asian and Middle East 
routes. <br>
<br>
<b>Two years ago, there was talk about Qatar Airways flying to New York. When will 
that happen?</b><br>
We have not announced which city we intend to serve in North America. We have 
plans to operate services to the US East Coast. The plans are in the pipeline 
for the third quarter of 2006. We take delivery of our first Airbus A340 
long-range aircraft later this year and this will be capable of flying non-stop 
between Qatar and North America.<br>
<br>
<b>2006 means the Asian Games for Qatar. You are working closely with the Doha 
Asian Games Committee to make the games a huge success. How is it going?</b><br>
Qatar Airways is the official airline of the 2006 Asian Games and we have many 
plans in place to help make the event a huge success. We will be laying on 
special packages for travelers from around the world – and in particular Asia – 
to travel to the Games in December. We will also be increasing frequency on many 
of our Asian destinations to cope with the huge anticipated demand for travel to 
Doha during this prestigious event.<br>
<br>
<b>Qatar Airways currently has one to two daily flights to Muscat. Do you have any 
plans to add a service to the important holiday destination of Salalah – direct 
or through a code share with Oman Air?</b><br>
We are always exploring new business opportunities and our commercial team in 
particular is evaluating a number of destinations to add to Qatar Airways’ 
international map. Oman is a strategically important country for Qatar Airways 
and we will continue to explore ways of increasing our presence in this vital 
market. <br>
<br>
<b>Skytrax has yet again voted your cabin crew as the best in the Middle East and 
Qatar Airways ranks in the top 10 of the best airlines globally. How are you 
maintaining or even improving on your high standards?</b><br>
When an airline wins awards, its focus is always on maintaining its 
competitiveness and awareness in the public eye. We, at Qatar Airways, continue 
to strive for excellence and maintaining the highest possible standards of 
service in the air and on the ground. We have a committed workforce that helps 
provide the recognition we deserve. <br>
<br>
<b>Some airlines, in particular the low-cost carriers, are steering away from 
luxury and service. Do you think First Class luxury and service will soon become 
a thing of the past?</b><br>
Much has been said about the future of First Class. Many airlines have removed 
this cabin, but Qatar Airways believes in service and offering a product for 
which there is demand. We have no intention of removing First Class. It is a 
unique product. The customer base is there to fly the service and we are 
extremely happy with the accolades received from around the world about our 
premium cabin.<br>
<b>
<br>
With such a high number of airlines operating in this relatively small region, 
why should the passengers from Oman choose to fly Qatar Airways?</b><br>
Qatar Airways focuses on service and on-time performance. We offer a highly 
competitive product. Passengers who fly with us will not want to fly any other 
airlines. Besides, we have a wider network of flights in and out of Doha than 
what is currently offered in Muscat. Flying Qatar Airways means passengers have 
an exciting array of destinations they can travel to via our Doha gateway. </font></p>

