<br>
<br>
</font><b><font face="Verdana" size="5">A City Car </font></b><font face="Verdana" size="1"><br>
<b><br>
<img border="0" src="car_talk.jpg" width="300" height="160" align="right" hspace="5" vspace="2">They 
say good things come in small packages. Nissan Tiida is no exception, as we find 
out after experiencing a test-drive</b><br>
<br>
</font><font face="Verdana" size="3"><b>W</b></font><font face="Verdana" size="1">hen 
a brand known for its impressive range of SUVs and mid-size sedans launches a 
small-sized hatchback, it makes one raise an eyebrow. After all, this is a 
market dominated by SUVs and mid-sized sedans. But Nissan’s latest offering, 
Tiida, sure seems to be catching the fancy fast. Spending a day with the Tiida, 
we realised size does not matter. The hatchback does pack in quite a punch. <br>
<br>
Nissan has also launched the sedan version of the Tiida and there are two 
variants each in both the models. We test-drove both the versions though much of 
our time was with the hatchback version as it has been creating quite a ripple 
in the market. Going by Nissan’s strong automotive history and impeccable 
engineering standards, it should not come as a surprise that the first small car 
to be launched by the automobile giant is a notable contender compared to its 
existing models though it belongs to a different genre.<br>
<br>
Both the versions of Tiida currently come with 1.6-litre engines but the 
automobile company will be introducing the 1.8-litre versions in the Sultanate 
very soon. There is little to complain about the 1.6-litre midget though. Here’s 
a peek into the features of the vehicle. <br>
<b><br>
Outside view</b><br>
The hatchback is undoubtedly a city car, not just in performance but in styling 
as well. A high-mounted LED stop lamp on the rear that alerts the drivers 
following your car when the brakes are applied, stylish wraparound headlamps, 
slightly protruding tail lights and clean, aggressive frame pep up the 
hatchback’s city car look. The rear window also has a wiper, a handy aspect in 
the hatchback. Though the Tiida hatchback is small in size, surprisingly, it 
does not have a stubby rear like most hatchbacks. Instead, it has a longer and 
firmly curved end. The Tiida hatchback comes in an impressive range of eight 
colours. As for the OER team’s favourites, they were the Sapphire Black and Red.
<br>
<b><br>
Inside view</b><br>
Hatchbacks are generally constricted when it comes to interior space. This is 
where the Tiida differs. The leg space is quite impressive. One of our tall 
friends, who usually make a fuss about leg space in the backseat inside sedans, 
was quiet for once. <br>
<br>
The interior design of the Tiida hatchback, which includes the dashboard with 
all its controls, is simple and fuss-free. One of the notable features of the 
Tiida is the placement of the boot and petrol tank controls. They are placed 
below the A/c vent on the left-hand corner of the driver. It gives one freedom 
from bending down or fumbling with the controls to open the boot or petrol tank 
top. Storage space gets in more brownie points for the Tiida hatchback. There 
are storage areas just about everywhere. Besides, the notably spacious glove 
compartment, Tiida has lidded stash away spaces on the dashboard and centre 
console; there are cup holders for rear seat passengers, console box inside the 
front armrest and also door pockets. Nissan means serious business when it comes 
to spaciousness even in a hatchback.<br>
<b><br>
On the road</b><br>
Quick, smooth and in control. That’s the first impression of the Tiida when you 
drive it on the road. The 1.6-litre engine puts up quite a performance. The 
mount up the Qurum Gardens roads was as smooth as it was when cruising on the 
straight roads of Muscat. The 4-speed automatic version that we took was fast.
<br>
<br>
Tiida moves with conviction and can zip through the increasing traffic of the 
city with absolutely zero stalling percentage. And the nimbleness and 
maneuverability of the hatchback impressed us a lot. For a small sized vehicle, 
the Tiida comes with superior aerodynamic design that guarantees nil front lift. 
As a result, you have better control even while traveling at higher speeds. <br>
<b><br>
Safety factor</b><br>
Nissan has not left anything to chance and included as many features as possible 
in the hatchback. For starters, the Tiida has a zone body construction with 
crumple zones in the front and rear. Two airbags have been deployed for the 
driver and passenger with front seat active head restraints to reduce the scope 
of injuries in the unfortunate event of accident. <br>
<br>
The pretty powerful headlamps ensure that your journey through dimly lit areas 
is safe, like we found out while cruising through the by-lanes of Muscat. <br>
<br>
To sum it up, the new Tiida will hold its ground in the land where SUVs and 
luxury cars have carved a niche for themselves. For not only is the Tiida neatly 
packaged with the trusted, reliable Japanese technical know-how, but it also 
features top of the range traits that are truly global.
</font></p>
