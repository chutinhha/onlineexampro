<br>
</font><font face="Verdana" size="5"><b>Bahrain shows the way</b></font><font face="Verdana" size="1"><br>
<br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="1" src="gcc_dairy1.jpg" align="left" hspace="5" vspace="2" width="192" height="157">T</b></font><font face="Verdana" size="1">he 
verdict is out. Bahrain remains the most economically free country in the Gulf 
Cooperation Council (GCC). The country maintains a pro-business environment with 
an excellent banking and finance system, low regulation, and low barriers to 
foreign investment according to the 12th Index of Economic Freedom 2006 
published jointly by the Heritage Foundation and the Wall Street Journal. <br>
<br>
However, despite its free economy, Bahrain receives over 80 per cent of its 
revenues from state-owned enterprises, mostly oil and gas. The report also 
considers Qatar as “mostly un-free” economy. However, Kuwait, Saudi Arabia and 
the UAE are termed as “mostly free” economies by the report.<br>
<br>
In the overall ranking, Bahrain stands 25th, followed by Kuwait at 50th, Saudi 
Arabia at 62nd, UAE at 65th, Oman at 74th and Qatar at 78th rank. Countries 
receive a 1-5 rating, with one being the best, on 10 broad measures of economic 
freedom: trade policy, fiscal burden of government, government intervention in 
the economy, monetary policy, capital flows, and foreign investment, banking and 
finance, wages and prices, property rights, regulation and informal market 
activity. The scores are averaged to create an overall score.<br>
<br>
Thanks to its relatively cosmopolitan outlook, modern economy, favourable 
regulatory structure, and excellent communications and transportation 
infrastructure, Bahrain has become an ideal base for multinational firms looking 
to set up business ventures in Persian Gulf. However, for the second consecutive 
year, the country declined in economic freedom. In general, the entire Middle 
East has experienced a net decline in economic freedom registering worse scores.<br>
<br>
<img border="0" src="gcc_dairy.jpg" align="right" hspace="5" vspace="2" width="350" height="199">Jasim Ali of University of Bahrain points out that Bahrain has lost five 
rankings in the 2006 Index of Economic Freedom. “The index rated Bahrain’s 
economy as mostly free, having achieved a score of 2.23 on the 5-point scale 
(the lower the better). “The country earned a grade of 2.15 in 2005. The 
downgrade reflected growing inflationary pressures,” says Ali. Another negative 
aspect, he points out, is Bahrain’s “worst result (4 out of 5) in the government 
intervention variable”. <br>
<br>
Overall, in all GCC countries, the economy has opened up to foreign investments 
with substantial barriers. If Kuwait restricts foreign investment in the 
upstream petroleum sector and does not allow foreigners to own real estate, the 
Bahraini government does not license companies wishing to compete with existing 
government-owned companies and established local enterprises.<br>
<br>
Qatar allows majority and full foreign ownership of businesses in agriculture, 
industry, health, education, tourism, and projects involved in the development 
of natural resources. Still, the law requires foreign businesses to employ a 
local agent. Foreign companies face a higher tax rate (up to 35 per cent) than 
do domestic companies (0 per cent). Foreign investment in the UAE is restricted, 
except for companies located in one of the free zones.<br>
<br>
Similarly, majority of GCC countries control prices and provide subsidies to a 
variety of items. In the same way, though these countries do not mandate minimum 
wages in the private sector, they do set wages in the public sector.<br>
<br>
As a general rule, government intervention is very high in GCC countries. 
Foreign investment limitations are moderate to high. The region has low fiscal 
burden and trade policy restrictions varied between moderate and high. Rest all 
categories experience only moderate government restrictions.- Sowmya Sri 
Rallabhandi</font></p>

