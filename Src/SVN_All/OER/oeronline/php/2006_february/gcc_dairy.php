                        <img border="0" src="qatar.jpg" align="left" hspace="5" width="76" height="70"><font face="Verdana" size="1"><b>IPO 
                        fever grips GCC</b><br>
                        January was buzzing. A hotel room was hard to find, and 
                        there were people sleeping in their pick-ups. All roads 
                        seemed to lead to Doha. And why did this tiny metropolis 
                        become so irresistible to GCC nationals? The Al Rayyan 
                        Bank IPO. For the first time ever, a local IPO was open 
                        to GCC nationals. <br>
                        <br>
                        Al Rayyan Bank has a capital of QR5 billion. Forty-five 
                        per cent of the proposed bank’s equity is held by 
                        promoters amounting to QR3.37 billion, while the 
                        remaining 55 per cent, or 4.13 billion, will be offered 
                        to the public. <br>
                        <br>
                        Of the promoters’ equity, 25 per cent will be retained 
                        by Qatari investors, whereas 20 per cent will be held by 
                        investors from other GCC states. As for the bank’s 
                        public equity, 80 per cent of it was earmarked for 
                        Qataris and the remaining 20 per cent to other GCC 
                        nationals. <br>
                        <br>
                        One of the two mega banks to be floated in Qatar, Al 
                        Rayyan Bank, launched an initial public offering (IPO) 
                        on January 15, 2006, and received overwhelming response 
                        from both Qatari and GCC citizens. Minimum subscription 
                        will be for 500 shares while maximum allotment will be 
                        50,000 shares.Though the IPO is open till January 29, 
                        sources say that it has been subscribed many times over.<br>
                        <br>
                        To say the response from GCC nationals was overwhelming, 
                        would be an understatement -- on one particular day 
                        there was even a mini-stampede and unruly scenes were 
                        reported at the Qatar Sports Club, where QNB, the lead 
                        arranger, had set up facilities for GCC investors. <br>
                        <br>
                        In the melee, a glass panel of a door of the main hall 
                        at the sports club was damaged. When the crowds became 
                        unruly special public security forces were deployed and 
                        they soon controlled the situation. <br>
                        <br>
                        Main entrances to the club were closed a little before 
                        noon with many in the crowd screaming to throw it wide 
                        open since every one seemed to be in a hurry to complete 
                        subscription formalities and leave Doha for home. <br>
                        <br>
                        Although people from different GCC countries came for 
                        the IPO, Saudis outnumbered all others. <br>
                        <br>
                        The promoters of Al Rayyan Bank are Qatari Diar (the 
                        newly established and state-sponsored real estate giant) 
                        and the Qatar General Retirement and Pension Authority. 
                        Qatari IPOs have so far been offered to nationals only. 
                        Another recent Qatari launch that attracted huge GCC 
                        interest was the Lusail project. <br>
                        <br>
                        The $5 billion project is already in the market, and has 
                        attracted wide interest from the region, especially 
                        Kuwait.</font></td>
