                        <p style="margin-left: 10; margin-right: 10; margin-top: 1; margin-bottom: 10">
                        <font face="Verdana" size="1"><br>
                        The year 2006 has brought double blow to the Gulf 
                        Cooperation Council (GCC) countries. First, it was the 
                        ruler of Dubai, Shaikh Maktoum Bin Rashid Al Maktoum, 
                        who passed away on January 4, 2006 at the age of 62. 
                        Then, within 10 days, the “Father of Modern Kuwait”, 
                        Emir, Sheikh Jaber Al- Ahmed Al-Sabah passed away on 
                        January 15, 2006.<br>
                        <br>
                        Shaikh Maktoum was also the Vice President and Prime 
                        Minister of the UAE. He was a unique leader who enjoyed 
                        a close bond with people and shared their dreams. 
                        Citizen’s welfare always topped his priority list. With 
                        an eye on the less affluent citizens, Sheikh Maktoum 
                        spearheaded a programme to construct thousands of new 
                        homes in projects throughout Sharjah, Ajman, Umm Al 
                        Qaiwain, Ras Al Khaimah and Fujairah, the other emirates 
                        of the UAE. In his 15 years of rule, Maktoum converted 
                        Dubai, the tiny Gulf emirate, into a world-renowned 
                        commerce and tourism hub of the Middle East. <br>
                        <br>
                        Sheikh Jaber is credited with transforming Kuwait from a 
                        small emirate into a modern nation-state. During the 
                        invasion of Kuwait in August of 1990, Sheikh Jaber stood 
                        like a pillar of strength and rebuilt the country. When 
                        he was the Minister of Finance and Economy, he 
                        established The Credit Bank, offering real estate, 
                        industrial and agricultural credit to citizens and 
                        businesses alike, issued a Kuwaiti currency (previously 
                        Kuwait used the Indian Rupee) and established a board to 
                        supervise the issuing of banknotes and coins. <br>
                        <br>
                        In 1961, with financial support from and under the 
                        direction of Sheikh Abdullah Al-Salem Al-Sabah, Sheikh 
                        Jaber established the Kuwait Fund for Arab Economic 
                        Development. The fund’s goal of fostering the economic 
                        and social development of fellow Arabs was an incredible 
                        achievement, particularly when you consider that Kuwait 
                        itself was still considered a developing nation at the 
                        time. Today, the fund has given grants to over 70 
                        countries in Latin America, the Caribbean, Central Asia 
                        and Europe, as well as Arab nations around the world for 
                        whom the fund was originally meant.<br>
                        <br>
                        In 2005, the shadow of grief engrossed Saudi Arabia. On 
                        August 1, 2005, the Kingdom lost the Custodian of the 
                        Two Holy Mosques King Fahd bin Abdulaziz Al-Saud. As 
                        King, he supervised projects to facilitate Hajj for the 
                        millions of pilgrims from around the world each year. 
                        Under King Fahd’s rule, Mecca was expanded to 3.5 
                        million square feet to accommodate one million 
                        worshippers. Medina has grown to nearly 1.8 million 
                        square feet to accommodate 500,000 people, according to 
                        his official biography. <br>
                        <br>
                        For the UAE, the death of Shaikh Maktoum came almost a 
                        year after the passing away of the visionary leader, His 
                        Highness Sheikh Zayed Bin Sultan Al Nahyan, President of 
                        the United Arab Emirates (UAE) and Ruler of the Emirate 
                        of Abu Dhabi, who died on November 2, 2004. Sheikh 
                        Zayed, who was instrumental in the formation of the UAE, 
                        brought a high standard of living that has spread 
                        throughout the seven emirates, from the ultra-modern 
                        cities to the remotest areas of desert and mountains.
                        <br>
                        <br>
                        The change has, moreover, occurred against a backdrop of 
                        enviable political and social stability, despite the 
                        insecurity and conflict that has dogged much of the rest 
                        of the Gulf region.<br>
                        <br>
                        The strength, vision and dedication of these leaders 
                        have transformed the Gulf into a major trade center in 
                        the international market.</font></td>
