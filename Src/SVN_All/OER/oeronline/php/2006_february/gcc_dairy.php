                        glitters, sales continue</b><br>
                        The cancellation of the Dubai Shopping Festival (DSF), 
                        following the death of Sheikh Maktoum Bin Rashidh Al 
                        Maktoum, has left mixed impact on the hotel and tourism 
                        industry of the Emirate. Dubai, a haven of shopping and 
                        a global tourist destination, usually attracts around 3 
                        million people during this annual event. <br>
                        <br>
                        Since its inception in 1996, DSF has drawn people from 
                        across the globe. The world’s best brands are available 
                        here for best prices during the one-month event with the 
                        motto - 'One Family, One World, One Festival'. Though 
                        the event has been put off this year, retail sales are 
                        allowed. DSF logos and messages will be removed from the 
                        products. It is expected that retail spending could 
                        cross Dh7.67 billion, about 7 per cent of Dubai’s 
                        Dh110-billion GDP. DSF 2005 has recorded 3.3 million 
                        visitors who spent Dh6.67 billion during the 32-day long 
                        festivities that translates to Dh2,021.21 average 
                        spending per visitor.<br>
                        <br>
                        In 2004, the tourism industry recorded 37 per cent 
                        growth in revenue from 5.42 million hotel guests, 
                        reaching Dh6.2 billion last year. Nevertheless, with the 
                        cancellation of DSF, many hotels reported drop in 
                        occupancy levels. Even the Eid booking did not meet the 
                        expectations . At the same time, the hospitality 
                        industry in general feels that Dubai, with or without 
                        DSF, continues to be a hot destination. The city has 40 
                        malls apart from the traditional souqs to attract 
                        tourists. <br>
                        <br>
                        The Dubai Gold &amp; Jewellery Group -- an association of 
                        more than 700 gold and jewellery members in Dubai -- has 
                        resumed its promotions, the jewellery campaign and the 
                        gold coin campaign, in the greater interest of consumers 
                        after DSF permitted private sector to run retail 
                        campaigns. So DSF might be cancelled technically, the 
                        shopping event still continues without its international 
                        theatre and street performances, fashion shows, nightly 
                        fireworks and laser shows, film festivals, children's 
                        events and handicraft shows.</font></td>
