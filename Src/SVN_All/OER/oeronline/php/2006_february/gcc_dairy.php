                        has reportedly recorded an exuberant growth for the year 
                        2005. According to a report released by Global 
                        Investment House, it is believed that the robust 
                        economic conditions and increased private spending as 
                        well as a strong pick up in gross fixed capital 
                        formation driven by several capital projects could 
                        result in double digit growth in 2005. <br>
                        <br>
                        The report attributed the high growth rate of Kuwait 
                        mainly to the ascending price of crude oil, throughout 
                        2004 and in 2005, thanks to the continued strong demand 
                        emanating from emerging economies and the damages caused 
                        by the hurricanes in the US. The rising oil prices not 
                        only helped the economy but also fostered growth 
                        prospectus in markets.<br>
                        <br>
                        <img border="0" src="saudia1.jpg" align="left" hspace="5" width="100" height="95">In 
                        contrast to a disappointing first half of 2004, the 
                        positive macro economic conditions and superior 
                        performance of the Kuwaiti companies have led to an 
                        improved market sentiment, thereby helping regain the 
                        confidence of investors. In terms of year-to-date 
                        performance, all sectors were on positive territory in 
                        the first ten months of 2005, growing at double-digit 
                        rates with insurance sector experiencing the lowest 
                        growth rate of 16.87 per cent. <br>
                        <br>
                        The Investment sector dominated the sector, gainers 
                        advancing by 108.20 per cent during the first 10 months 
                        of 2005, much higher than the 17.32 per cent 
                        appreciation for the whole year of 2004. The banking 
                        sector – the largest of all sectors – too finally caught 
                        the fancy of the investors after a period of 
                        under-valuation to record a gain of 49.9 per cent, the 
                        report stated. <br>
                        <br>
                        In a scenario of rising oil pries, trade surplus for 
                        Kuwait reached an all time high of KD3.855 billion in 
                        the first half of 2005. Based on this, trade balance is 
                        expected to rise to a level of KD7.7 billion in 2005 
                        implying 35.5per cent growth over 2004. This is the 
                        direct result of imports growing faster at 27.7per cent 
                        on an annualized basis in 2005 compared to 9.3 per cent 
                        in the previous year, and exports slowing down to 32.7 
                        per cent from 37.1 per cent growth in 2004, the report 
                        says.</font></td>
