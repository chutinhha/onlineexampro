                        <img border="0" src="saudia.jpg" align="left" hspace="5" width="143" height="64"><b>Sparking 
                        stocks</b><br>
                        Saudi Arabia’s equity market had an impressive year in 
                        2005 and is heading for a fruitful 2006. Saudi’s All 
                        Share Index closed at 16,712.64 points by the end of the 
                        2005 compared to 8206.23 for 2004, an increase of 103.66 
                        per cent. The establishment of Capital Market Authority 
                        (CMA) and the country joining the WTO has helped pep up 
                        the Saudi stock market. <br>
                        <br>
                        The total shares trade rose by 19.26 per cent, reaching 
                        12.28 billion, compared to 10.3 billion for 2004. A 
                        total of 46.61 million transactions were executed during 
                        2005 compared to 13.32 million for 2004, recording a 
                        huge rise of 250 per cent. CMA Governor Jemaz Al-Suhaimy 
                        told delegates at a recent stock market conference in 
                        Riyadh that the Saudi bourse is 16th among the world’s 
                        50 largest stock markets. “The market grew by an average 
                        of 38 per cent annually and 95 per cent this year,” he 
                        added.<br>
                        <br>
                        According to a report by National Commercial Bank of 
                        Saudi, since the end of 2000, investors in the Saudi 
                        equity market reaped a 495.8 per cent gain on the rise 
                        in share prices — and that is besides the dividend 
                        payments of around 2.5 per cent annually and around one 
                        per cent from the distribution of free bonus shares. <br>
                        <br>
                        It has been a good time for investors on the Saudi stock 
                        market. The overall wealth of the investments increased 
                        by well over 500 per cent during the last five-and-half 
                        years through to June 2005.</font></td>
