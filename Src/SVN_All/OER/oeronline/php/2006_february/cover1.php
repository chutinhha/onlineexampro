            <p align="justify"><br>
			<b><font face="Verdana" size="2">Sixth 5-year Plan (2001-2005)</font><font face="Verdana" size="1"><br>
			</font><font face="Verdana" size="5">Firm progress towards Vision 
			2020</font></b><font face="Verdana" size="1"><br>
			<br>
			<b>Ahmed bin Abdulnabi Macki, Minister of National Economy and 
			Deputy Chairman of Financial Affairs and Energy Resources Council, 
			reviews the performance of the Sixth Five-Year Plan.</b><br>
			<br>
			</font><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
			most important objective of the Sixth Five-Year Plan was to achieve 
			stability of the per capita income, adopting sustainable fiscal 
			policies, employment of the citizen, expanding and upgrading 
			education, expansion of the higher education, enhancing the rate of 
			the diversification processes, development of the private sector 
			through encouragement of its investments, and activation of the 
			privatisation processes. <br>
			<br>
			On the performance front, despite the decline in the actual 
			production rates of oil to levels below the Plan by about 14 per 
			cent, the noticeable increase in actual oil prices compared to the 
			Plan by 75 per cent has led to increase in the government revenue. 
			Such increase resulted in improvement of the financial position of 
			the government. The general budget realised surplus compared to the 
			planned deficit. The government’s financial reserves have also been 
			enhanced, and there was a decrease in the volume of public debt and 
			its structure improved as a result. In light of all these, the 
			credit worthiness of the national economy witnessed an improvement.<br>
			<br>
			The gross domestic product (GDP) at current prices is expected to 
			register an average growth rate of 8.7 per cent, compared to the 
			planned annual rates of around 0.6 per cent. The national economy 
			registered low inflation rate with an annual average not exceeding 
			0.3 per cent despite the increase in prices in 2005 due to the drop 
			in the exchange rate of the US dollar, relative to the other foreign 
			currencies, and the price increase in the international markets. <br>
			<br>
			Many positive aspects have characterized the Plan’s performance 
			related to the economic diversification. The non-oil activities 
			revealed noticeable recovery. Its value, added at current prices, 
			increased at an annual rate of about 9.2 per cent on an average. In 
			light of that, the annual average of its actual value added for the 
			Plan period increased by 7.5 per cent relative to the Plan. <br>
			<br>
			The natural gas-based industries registered high growth rates with 
			an annual average of about 36.8 per cent and accordingly, the annual 
			average of its actual value added increased by 62.8 per cent above 
			the plan. Besides that, the government approved an incentive 
			package, including low price of natural gas inputs, provision of 
			world-class port services, and tax exemption for a renewable period 
			of five years, for encouragement of foreign capital to invest in the 
			natural gas based industrial projects. This has led to the 
			commencement of implementation of many of these projects whose total 
			cost is estimated to be about US$ 13 billion.<br>
			<br>
			The tourism sector witnessed remarkable development with its value, 
			added at current prices, growing at an annual rate of about 6.7 per 
			cent on an average. This led to an increase in its annual average by 
			1.9 per cent above the Plan. The total number of hotel rooms 
			increased from 5312 in 2000 to 8082 in 2005. <br>
			<br>
			The non-oil merchandise exports’ (including the re-exports 
			operation) annual proceeds increased from RO 926 million in 2000 to 
			RO 1889 million in 2005 registering an average annual growth rate of 
			about 15.3 per cent. Its structure also improved in favour of the 
			exports of Omani origin, at the expense of the re-export operations.
			<br>
			<br>
			The private sector contribution to GDP increased at an average 
			annual rate of about 8.5 per cent. The private sector investment 
			increased from RO 297 million in 2000 to RO 398 million in 2003. 
			They are expected to reach RO 646 million in 2005. The sector has 
			also been able to provide an appropriate volume of employment 
			opportunities for the national labour force. In order to provide 
			favourable environment for the private sector, the government took 
			several measures and enacted legislations in order to upgrade the 
			regulatory and legal frameworks of the sector.<br>
			<br>
			On the side of enhancement of the privatisation programme, several 
			achievements have been made during the Plan period. The most 
			significant of these include the issuance of the Royal Decree No. 
			(77/2005), ratifying the privatisation policies and regulations, the 
			privatisation strategy of electricity and related water sector, and 
			the restructuring of the telecommunications sector. In addition, the 
			government shares in some companies were partially sold. <br>
			<br>
			The Omanisation rate in the private sector increased from 16.4 per 
			cent in 2000 to 25 percent in 2005. In the government civil service, 
			the Omanisation rate increased from 74 per cent in 2000 to 81.8 
			percent in 2005.<br>
			<br>
			In the education sector, the number of students enrolled in the 
			government general education was raised to about 626,000 students in 
			2004. The Plan period witnessed a remarkable decrease in the 
			illiteracy rate (according to the 2003 population census) to 22 per 
			cent of the population (15 years &amp; above), compared to 41.3 per cent 
			in 1993. Illiteracy among those below 40 years age reached about 6.4 
			per cent. Student enrolment in higher education increased from about 
			38,000 in 2000 to 60,000 in 2004. <br>
			<br>
			The total expenditure on the education sector increased from RO 398 
			million in 2001 to about RO 717 million, according to the amended 
			budget for 2005. Therefore, the total expenditure on education 
			during the Plan period increased to about RO 2.5 billion. <br>
			<br>
			On the health sector front, the health status indicators improved 
			during the Plan period. Infant mortality rates decreased from 16.7 
			per thousand live births in 2000 to about 10.3 cases in 2004. As a 
			result, life expectancy at birth increased from 73.4 years in 2000 
			to about 74.3 years in 2004. <br>
			<br>
			The Plan’s achievements, with regard to the improvement of the 
			living standards of the citizens and social welfare, included an 
			addition of about RO 45 million for construction of 3,889 social 
			housing units in the various Wilayats.</font></p>
