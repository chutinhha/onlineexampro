                <p align="left" style="margin: 10" dir="rtl"><font face="Verdana" style="font-size: 8pt">
                <b>Cancer not linked with mobile usage</b></font></p>
                <p align="center" style="margin: 10" dir="rtl"><font face="Verdana" style="font-size: 8pt">
                <b>&nbsp;<img border="0" src="ws1.jpg" width="100" height="85"></b></font></p>
                <p align="justify" style="margin: 10" dir="ltr"><font face="Verdana" style="font-size: 8pt">
                The regular mobile phone users are not at greater risk of 
                developing the most common type of brain tumor, glioma, 
                according to a study done in association with the Institute of 
                Cancer Research in London. The four-year study was conducted on 
                966 adults diagnosed with glioma. <br>
                <br>
                A group of 1,716 healthy volunteers was used as a control group. 
                The study found that there was no relationship between the risk 
                of glioma and the time since the adults’ first use of a cell 
                phone, the number of years they had been using a cell phone, the 
                number of calls they made, and the hours of mobile phone usage.<br>
                <br>
                However, the researchers did find a significantly increased risk 
                for tumors that developed on the same side of the head as where 
                the adults said they held their cell phones and a decreased risk 
                of tumors on the other side of the head.</font></td>
                <td width="28%" valign="top">
                <p style="margin: 10" align="justify"><font face="Verdana" style="font-size: 8pt">
                <b>China to develop<br>
                artificial sun<br>
                </b><br>
                A Chinese research institute is on its way to become the world's 
                first facility to development capabilities for 
                all-superconducting non-circular section nuclear fusion 
                experiments, which is generally known as the 'artificial sun'. 
                The claims are stupendous in nature as they relate to producing 
                energy from seawater through nuclear fusion. The claims remain 
                to be verified by the world scientific community. <br>
                <br>
                If true, it would be another feather in the cap of the state-run 
                Chinese Academy of Sciences, which has come up with a string of 
                scientific innovations and discoveries in the past one year. The 
                &quot;artificial sun&quot; success comes from the Hefei Institutes of 
                Physical Science, one of the dozens of facilities under the CAS, 
                which expects to complete its Experimental Advanced 
                Superconducting Tokamak (EAST) by next March or April. Once 
                done, it will begin exploring clean energy resources through 
                nuclear fusion, local media said.<br>
                <br>
                The focus of the experiment would be to extract of deuterium 
                from seawater and then producing energy from it through nuclear 
                fusion. Work in this direction began in 1991 in China when the 
                CAS Institute of Plasma Physics built the country's first 
                superconducting tokamak equipment HT-7.</font></td>
