<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<font face="Verdana" size="4">LEADING FROM THE FRONT</font></b><font face="Verdana" size="1"><br>
<b>HE Khalil al Khonji, Chairman, Oman Chamber of Commerce and Industry is in 
the thick of action, as usual</b><br>
<br>
<img border="0" src="news.jpg" align="left" hspace="8" vspace="6" width="200" height="197">Trust HE Khalil al Khonji, Chairman, Oman Chamber of Commerce and Industry to be 
a bundle of energy. The month gone by saw him leading the effort to ease 
restrictions on trucking companies to ply on Muscat’s bridges. With effect from 
August 1, trucks not exceeding 25 tonnes (combined weight of truck, trailer and 
cargo) have been permitted to use of bridges within the capital area as they ply 
to and from Port Sultan Qaboos. The gesture has been welcomed by trucking 
companies, who have long complained that traffic restrictions and bottlenecks 
have significantly constrained truck turnarounds, consequently hurting 
operational efficiency and revenue earnings. As a result of these curbs trucks 
operating to and from the port can only make a single round trip per day, as 
against the norm of two round trips in the past. Concerned that the hurdles 
faced by trucking companies would have an impact on the availability of 
essential goods within and outside the city the OCCI discussed the issue with 
the authorities concerned and got the go ahead for 25 tonne trucks. HE Khonji 
has rallied wholesalers of food items to come up with special Ramadan family 
packs priced at a discount for poor families within the Sultanate. The measure 
is sure to ease the price burden on a number of families, helping them enjoy the 
festive season. </font></p>
