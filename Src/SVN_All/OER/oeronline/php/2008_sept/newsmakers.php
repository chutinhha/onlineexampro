<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">WALKING THE TALK <br>
</font>
</b><font face="Verdana" size="1"><b>HE Darwish Ismail al Balushi, Secretary 
General of the Ministry of Finance is ensuring that the sale process of Omantel 
moves at a fast clip</b></font><br>
<font face="Verdana" size="1"><br>
<img border="0" src="newsb.jpg" align="right" hspace="8" vspace="6" width="200" height="183">The Ministry of Finance has short listed eight telecom operators to participate 
in the next phase of the sale process of Omantel. The ministry together with its 
advisers is in contact with the qualifying telecom operators that will be 
invited to submit the first round of technical and financial proposals for a 25 
per cent stake in Omantel. This includes prominent European, Asian and regional 
telecom operators. HE Darwish Ismail al Balushi, Secretary General of the 
Ministry of Finance and Chairman of the Steering Committee overseeing Omantel’s 
strategic partnership process can take a large part of the credit for the 
alacrity with which the process has moved. Says HE Al Balushi, “The quality and 
number of expressions of interest received were very strong. The level of 
interest is testament to the quality of the Omani macroeconomic scene, the 
opportunity in the Omani telecom market and of Omantel itself.” The ministry 
invited expression of interest from suitably qualified strategic partners and 
invited them to bid for an interest representing 25 per cent of Omantel’s issue 
share capital on July 7. In conjunction with the sale, the government said it 
would also consider providing the selected strategic partner with certain rights 
enabling it to increase its economic and voting exposure to Omantel in future.
</font></p>
