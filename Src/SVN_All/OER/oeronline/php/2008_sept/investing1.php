<img border="0" src="investing5.jpg" align="right" hspace="6" vspace="4" width="125" height="148">Investment priorities differ from person to person, but if a person saves 
regularly and invests prudently then it is enough to take care of most of his 
needs. As parents all of us want to give the best to our children. Ones wishes 
can come true if one exercises financial planning and discipline. If a kid’s 
education is important, then saving for it should be on top of ones agenda. In 
our parlance we call it planning from, ‘diaper to degree.’ One needs to start 
saving from the very beginning. An education degree in the US can set you back 
by US$25,000 per year and so one needs to be prepared.<br>
<br>
<b>Asset allocation <br>
</b>Insurance is one of the first things that a person should get in life. This 
cushions the impact of any untoward incident that leads to the death of the 
earning member of the family. If one is planning to put a certain amount of 
money as savings deposit in a bank like RO1,000 then we suggest to put RO950 in 
a bank and the remaining RO50 in insurance. This keeps the RO950 intact while 
ensuring that in case of any calamity the family gets an assured sum of money. 
‘A fixed deposit matures by time but insurance matures by event.’ The other 
advantage with insurance is that it forces one to save. There are education 
plans which give you the amount of money that is required to fund a child’s 
college education. If something happens to the breadwinner the future premiums 
get waived off but the child still gets the sum assured to fund his education, 
guaranteeing his future. Thus a person can look at a personal insurance that 
takes care of incidents like the loss of life, accident, sickness and 
unemployment (LASU). In addition one can take an education plan for ones 
children. A family needs to have a certain amount of money as a fall back for 
any small time contingencies like a loss of employment. So it is advisable to 
keep some money as a savings deposit in a bank. These two instruments (insurance 
and bank savings) are a must for any family and the base of financial planning. 
Whether one chooses other instruments or not is a matter of discretion.
<img border="0" src="investing6.jpg" align="left" hspace="6" vspace="4" width="294" height="288"><br>
<br>
The other savings options include investing in equities or mutual funds 
depending on ones preferences, real estate investments, gold and investment in a 
business. What proportion of ones income should go into each of these categories 
is a matter of debate, but as a planner I suggest that one should save about 40 
per cent of ones income. Out of this 12-15 per cent should go for insurance and 
five per cent into fixed deposits. The rest can be invested in other options 
like real estate, equities, mutual funds, jewellery etc. a number of mutual 
funds in developing countries like India have given good returns in the last 
five years and are worth looking at. Unit Linked Insurance Plans combine 
insurance and equity returns giving investors the best of both the worlds. 
Finally I would say man is a salve of circumstances so it is better to plan and 
live a life of dignity. <br>
