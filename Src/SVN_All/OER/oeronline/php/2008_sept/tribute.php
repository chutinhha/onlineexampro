<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">Entrepreneur Par Excellence<br>
</font></b><font face="Verdana" size="1"><b>As a tribute to Sheikh Saud Salim 
Abdullah Bahwan Al Mukhaini, the visionary and philanthropist who passed away on 
August 20, 2008, we reprint an artcle from OMAN 2006 our annual publication in 
which he talks about his life and vision</b></font></p>

<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="tribute.jpg" width="350" height="169"></p>

<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">Young nation. Long list of personal desires with purchasing capacity. A new 
ruler on the throne, full of ambition to make his people happy and prosperous. 
What more one can ask for, if you’re an entrepreneur? This precisely is what 
happened in the Sultanate of Oman in the early 1970s when His Majesty Sultan 
Qaboos bin Said ascended the throne. Businessmen with an uncanny business acumen 
walked in to cash in on the opportunity. Sheikh Saud Salim Bahwan, the founder 
of the Saud Bahwan Group empire, is one such genius. Just three decades down the 
line, he manages a multi-billion rial business enterprise – serving people with 
a smile and earning the respect and praise of his foreign partners.</font></p>

<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="tribute1.jpg" width="350" height="273"></p>

<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">If the world is tough, the world of business is even tougher. The state of the 
economy actually does not count for much. Be it a new nation or a mature one, 
the challenges are always there. Successful businessmen are those who possess a 
driving force, an innovative mind, leadership skills, a love for adventure and a 
passion to win. Nothing less will suffice. These ingredients will make or mar 
one’s career. Sheikh Saud Salim Bahwan, who has established a mammoth business 
enterprise in the Sultanate of Oman, is one such rare personality. <br>
<br>
Childhood was not a bed of roses for him. At the age of nine, he got himself 
engaged in trading on a wooden dhow. His entrepreneurial streak was unmistakable 
even at that tender age. Such a drive coupled with a passion for excellence saw 
him create a name for himself at home and abroad.<br>
<br>
Young nation. Dynamic leadership. Aspiring natives. Long list of demands. 
Indeed, an excellent combination for entrepreneurs to thrive. Sheikh Saud was 
one of the early business heads to spot this changing landscape. His magic touch 
led to repeated success stories - in every sphere of activity, making the Saud 
Bahwan Group (SBG) of companies one of the largest and the most successful in 
Oman. <br>
<br>
<b>First Break</b><br>
The Group is one of the largest business houses in Oman today. But the going was 
tough in the initial years. The shop at ancient Muttrah Souq where it all began 
was a very small outlet. He began by selling Toshiba TVs and Seiko watches. With 
the infrastructure development in full swing in addition to the oil boom in the 
1970s – there was an air of optimism.<br>
<br>
Sensing the mood of the nation, Sheikh Saud imported colour television sets and 
glitzy watches. What is noteworthy is his obsession with excellence. He chose to 
bring in the best for his countrymen – that too, from Japan, the tiny nation 
that was – and is – synonymous with quality electronic goods globally.<br>
<br>
As business grew, Sheikh Saud wanted to expand his horizon. Watching the world 
speeding ahead fast, he set his sights on acquiring the Toyota agency, which he 
was convinced would be a huge winner. The reason was simple: personal mobility 
was becoming a necessity in the absence of any public transport for people from 
various walks of life.<br>
<br>
However, Toyota was hesitant to give him the agency due to the small size of his 
business operations. He persisted in his attempts to win over Toyota Motor 
Corporation (TMC) and finally in 1975, his efforts bore fruit. The first order 
was for just five cars, but imports into Oman now have crossed well over 650,000 
units. <br>
<br>
<b>Dynamic Initiatives</b><br>
The Group’s leadership position has been maintained due to the pioneering 
‘customer firsts’ in marketing, after sales support and customer care. The 
Group’s huge investments have included building a world class sales, parts and 
service infrastructure. The Toyota service facility at Muscat is one of the 
largest Toyota facilities in the world. Recognising the growing, undisputed 
leadership of Toyota brand in Oman, Sheikh Saud was presented twice with the 
‘Best Management’ award — a first amongst 175 Toyota distributors across the 
world; the Group has also been consistently ranked amongst the world’s top 5 
distributors by TMC. Recently, Dr Shoichiro Toyoda, Honorary Chairman of TMC, 
himself presented Sheikh Saud with the ‘Toyota Excellence Award’ foroutstanding 
performance.</font></p>

<p align="center" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<img border="0" src="tribute2.jpg" width="414" height="493"></font></p>

<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<b>Diversification</b><br>
The group strongly believes that diversification into new areas is the key to 
rapid growth and success. It not only broadens horizons but offers new 
challenges also. Emerging Oman offered plenty of scope for alert business groups 
to thrive and prosper. The Group has gone into Automobile Ancillaries, 
construction equipment; oil &amp; gas products, specialty equipment and 
infrastructure projects, defence equipment and services, travel and tourism, 
among others. <br>
<br>
A classic example of their ability to service and supply exacting requirements 
would be the multi-million dollar contract with Petroleum Development Oman for 
the supply and maintenance of 600 specially modified vehicles; and the 
purpose-built fuel tankers for Shell Oman.<br>
<br>
<b>Omanisation</b><br>
The oft-quoted expression of the Chairman is “We don’t seek satisfaction in mere 
numbers. We take pride in creating rewarding careers for Omanis.” This 
expression encapsulates the Group’s commitment to Omanisation … a vital national 
priority, in which it has taken giant strides. Sheikh Saud himself supervises 
the Omanisation programme. Good work does not go unnoticed. The Omanisation 
drive has earned it a number of accolades. These include the coveted Green Card 
in recognition of the Group’s outstanding Omanisation record for the last seven 
years in a row, the GCC Award for Excellence in Omanisation; the prestigious 
Omanisation Award from the Ministry of Manpower and many more.<br>
<br>
All said and done, nation building is nothing but capacity and wealth creation 
on a large scale. This can happen only if there is an ardent, collective desire. 
It is inevitable and desirable as well that mega corporations sprout in the 
process of such frenetic nation building activity. Sheikh Saud took the 
challenge of nation building by bringing world class brands, consumer items and 
cars to Oman and creating job opportunities for the Omanis.<br>
<br>
Even before corporate social responsibility became a buzzword, Sheikh Saud 
Bahwan was into it, in his own way. </font></p>

