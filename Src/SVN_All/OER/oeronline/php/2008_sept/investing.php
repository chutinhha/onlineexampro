<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">A bright future<br>
</font></b><font face="Verdana" size="1">
<b>A well planned approach towards our finances can make our children’s journey 
to adulthood that much easier and better. Mayank Singh reports</b><br>
<br>
Can you put a price on the expression of pure bliss on your three year old’s 
face as she enjoys an ice cream. When your 17-year old jumps in ecstasy on 
hearing the news of having secured admission in his dream college, would a 
parent rack his brains at the amount of money that it is going to cost? These 
are non- questions for any parent. Parental love is unconditional and largely 
unaccountable. It is heartless and clinical to count you child as a cost centre. 
But it also goes without saying that financial planning for kids is the first 
step to give them an advantage in life. <br>
<br>
The dichotomy of spending on your children is a conflict between the present and 
the future. Should you give in and buy him the RO200 plus Play station 3 that 
your son has been craving for or should you save for his graduation? Will the 
Barbie themed RO500 party on your daughter’s birthday be the reason why she will 
have to do her MBA in Muscat and not in New York. The only way to solve these 
dilemmas is to plan ahead and start investing now.<br>
<br>
<b>Bundle of joy </b><br>
<img border="0" src="investing.jpg" align="left" hspace="6" vspace="4" width="250" height="249">The pleasure of holding ones new born baby in an experience unlike any other 
feeling in the world. A smile from ones kid is enough to wipe off the blues of a 
bad day at office or the sleepless nights spent on carrying her around. It may 
probably be the best time to start ones journey on the road to financial 
planning. Khalil Ahmed al Harthy, Assistant General Manager, Al Ahlia Insurance, 
says “History has shown us that with ever increasing inflation the nominal value 
of money goes down and it is the ethical obligation of a parent to lift their 
kids from any financial burden.” The birth of his daughter Teeb a year and a 
half back has made Khalil committed to giving her a secure future. Aware that 
their expenses will increase as she grows, he has invested in a property in her 
name. In addition he has created an equity fund to which he contributes 
something every quarter. “We want her to be able to sustain herself 
independently when she grows up.”<br>
<br>
Being young Khalil is aware that he can invest in high-risk, high return 
instruments at this stage, but he is averse to choosing risky options as he 
believes, “You cannot jeopardise the returns for your kids.” With a number of 
options available, he feels that there is a need to popularise the savings 
options for kids in the market.<br>
<br>
<b>The second coming </b><br>
Most couples can afford one child and want to do the best for him or her. As 
financials decisions go the second child is usually the one that swings the 
balances. The thought of having to keep away double the amount of what you need 
for a child can be daunting. Often, when kids are young, one plus one does not 
add up to two – you could reuse and recycle and keep your expenses slightly 
lower. But as they grow older, two children can be a strain on the finances. But 
with careful planning it can be taken care just like Rajeev Singh, partner, 
Transaction Advisory Services, Ernst&amp; Young and his wife Dhanashree Singh have 
done.<br>
<br>
A B-school graduate and a successful account planner in a prestigious 
advertising agency in India, Dhanashree took a conscious decision not to go back 
to a job once she got married and relocated to Oman, in 1996. Her decision was 
driven partially by the lack of exciting opportunities and partly by her desire 
to be a full time parent. <br>
<br>
The arrival of Jiya, the couple’s first child in 2000 gave her a chance to give 
her bundle of joy the security of a full time parent. She felt that the 
financial loss of not taking up a job was more than compensated by the 
satisfaction of looking after her child. <br>
<br>
<img border="0" src="investing1.jpg" align="right" hspace="6" vspace="4" width="300" height="201">A first-born usually brings its own set of anxieties and the Singh’s were no 
exception. Says Rajeev, “We were quite anxious about her well being.” Jiya’s 
grandparents set in motion the process of financial planning for the baby by 
earmarking a certain amount of money for her. The fixed deposit has a lock in 
period of 15-20 years and is expected to grow into a reasonable sum over these 
years. Rajeev on his part has taken an equity linked insurance policy with an 
annual contribution. The policy gives the family the security of an assured sum 
in case of any unforeseen calamity plus an average to high returns on the 
investment.<br>
<br>
A savvy investor, Rajeev has gone on to invest in sophisticated financial 
instruments like real estate trusts, energy based funds etc. “Initially it is 
about building up an egg nest which can be done by investing in fixed deposits 
and real estate. Once a certain threshold is achieved then one can look at high 
risk-high return areas.” Education and health of their children are the two big 
priorities for the Singh’s. Says Rajeev, “If she has the aptitude and potential 
to pursue a certain course be it engineering, medical or a vocational pursuit 
like learning acting in New York then she should not be held back because of the 
money.” <br>
<br>
Having been through the experience once most parents are more confident by the 
time they have their second child. So though the birth of Arhaan, the Singh’s 
second child brought its own set of financial implications, the couple has gone 
about things in more casual and matter-of-fact manner. “Though we realise that 
our savings need to be higher now, we have been less fussed and the attempt has 
not been as conscious as in Jiya’s case,” says Rajeev. A believer that one 
should not bequeath all of ones wealth to ones kids while alive, Rajeev and 
Dhanashree are working on giving their kids a secure future through some 
sensible planning.<br>
<br>
<b>The wonder years<br>
<img border="0" src="investing2.jpg" align="left" hspace="6" vspace="4" width="300" height="222"></b>12-year-old Jehan, has an eye for art and design. She has recently designed 
the décor for her own room. Convinced about her talent, Nabeel Jawad Sultan, her 
father and Director, Jawad Sultan Enterprises has started taking her opinion in 
design matters. Says Nabeel, “What she wants to pursue in life whether it is 
design, engineering or medicine is her choice. The only thing that I would like 
her to do is to get a minor degree in business as it will help her 
professionally.”<br>
<br>
An informed investor Nabeel started a Systematic Investment Plan (SIP) for his 
three kids Jehan, and the eight year old twins Layal and Jaifer from 2005 
onwards. He has also created an equity based portfolio for his children which he 
manages on their behalf. Says Nabeel, “Since these investments are for you 
children the investment vehicles need to be conservative. If one is not a very 
well informed investor then one should leave the choice of investments to 
professionals.” He expects to earn a return 9-12 per cent on these investments. 
Equities and real estate are the two investment options that he prefers for his 
children. “My philosophy is that one should plan for the worse. As a parent the 
two things that one can give their children is a good education and savings,” 
says Nabeel. <br>
<br>
He has also been working on inculcating financial discipline and awareness in 
his kids. Thus Jehan and Layal know their bank account numbers. Whenever they 
break an expensive toy, the loss gets deducted from their savings while the 
money that they get on Eid and other festivities from elders gets added to their 
respective accounts. Goodies like a playstation or an expensive gift does not 
come easy as they have to convince dad about the acquisition. Not an easy task 
as Nabeel takes upto three weeks before relenting to any demand. <br>
<br>
While he is a stickler for financial discipline, when it comes to co-curricular 
activities Nabeel is more than happy to indulge his children. Jehan goes for 
dancing lessons, Layal takes piano, violin and bale classes while Jaifa has gone 
in for Karate and football. “These are important to develop their personality 
and so one should allocate a certain part of ones finances towards these 
expenses.”<br>
<br>
<b>Early teenage</b><br>
<img border="0" src="investing3.jpg" align="right" hspace="6" vspace="4" width="275" height="346">“I spoil my children by buying them everything that they want,” confesses David 
Aziz, Director – Automotive Products, Mohsin Haider Darwish. He rationalises 
this by adding that subconsciously this is an attempt to make up for the time 
that he stays away from his family due to the demands of his high-pressure job. 
So while he is planning to fulfill the latest wish of a pink laptop that his 13 
year old daughter Tara has made his wife Ban Aziz has been telling him to be 
more prudent. <br>
<br>
Though an indulgent parent David is conscious about the need to be financially 
cautious to give his children a good future. A firm believer in the power of 
compounding he has opened a savings account for all his children from the day 
they were born. A certain amount of money is debited from his bank to this 
account every month. As his financial standing has grown he has increased the 
amount that he debits each month to these accounts. There are times when he asks 
his bank to top up these accounts with additional sums either to take care of 
prevailing inflation or just because he has some cash to spare. “We review our 
financial plans on a yearly basis and see whether they require any changes,” 
says David. The yearly exercise is when David and Ban do a status check of their 
financial necessities and optionals like going on a holiday or buying big ticket 
items. <br>
<br>
The arrival of a kid is the biggest joy in any parent’s life but it also brings 
in its own set of challenges from financial expenses to other commitments. “When 
Tia (their second child) was born my wife was a senior school teacher in science 
in UK. But once we factorised the amount that we were spending on their day care 
and crèche facilities, she felt that it was not worth continuing and she gave up 
her job to give our children a good growing environment.” Financial planning for 
kids is thus a moving goal post wherein ones plans needs to change with each kid 
and as they go through different life stages like toddler years, pre school, 
school, pre teens, teenage years etc. <br>
<br>
With just four to five years left for Tara to go to college, David and Ban are 
aware that they need to have a corpus to fund Tara’s dream of becoming a doctor. 
“Tara wants to follow in the footsteps of her maternal uncle and aunts all of 
whom are reputed doctors in the US and UK,” says David. A medical degree in the 
UK, though does not come cheap – it entails a yearly expense of £10,000, so by 
the time one is through college it could amount to £60,000. Though there are 
student loans that the UK government gives out at preferential rates, David is 
not keen on availing one as it would mean Tara beginning her career mired in 
debt. The Aziz’s are confident that their savings over the years and some 
investments in mutual funds, equities and properties will enable them to give 
their children a good education.<br>
<br>
Seeing people in Oman living beyond their means David feels that with banks and 
other financial institutions being too eager to give loans it is important for 
Omanis to be more aware financially. “While borrowing money it is important to 
take into consideration the financial implications like knowing whether one can 
repay the sum without jeopardising ones kid’s future.”<br>
<br>
<b>The last mile</b><br>
<img border="0" src="investing4.jpg" align="left" hspace="6" vspace="4" width="250" height="315">“It is important to make children realise early in life that the sun does not 
shine everyday,” says Mohammed Mubarak Al-Shikely, Senior Manager Marketing, 
Oman Air. While Mohammed has been trying to inculcate the values of patience and 
thrift in his children, he has also been investing sensibly to take care of 
their future. <br>
<br>
A savings plan has been initiated for his five kids – Mubarak, Soud, Faiz, Ahmed 
and Salwa. He consulted a number of companies like HSBC, Alico, BankMuscat 
before selecting the ones which suited the needs of each of his children. Apart 
from these he has made some real estate investments as a cushion for them. 
“Property never lets you down,” says Mohammed. His recently built farm house in 
Seeb proved to be a grounding of sorts in financial affairs for 13-year old 
Mubarak and 12-year old Soud. They were interested in knowing how the house was 
built, the material cost etc. <br>
<br>
Though Mohammed has some investments in stocks, he sees them as instruments for 
short term gains and not as an option for his children’s future. As Mubarak 
steps into teenage years his career choices seem to change almost every day. 
Says Mohammed, “One day he wants to be a pilot, another day a doctor and then an 
engineer.” But with Mubarak a few years away from a college education the 
Shikely’s have plans to send him abroad to get a university degree. “We want him 
to go abroad as the experience of living on ones own is a learning process. It 
is more about developing oneself beyond the classroom.” <br>
<br>
Like most other parents, the Shikely’s do not see marriage as a big expense. 
They believe that it is a discretionary expense which varies from person to 
person. “If it is a choice between saving for a kid’s education or his marriage 
then it is not even a close race,” says Mohammed. Finally he feels that parents 
looking at financial planning should not jump at the first piece of advice that 
they get but check out the various options in the market and that expenses 
should be prioritised in their order of importance. <br>
<br>
Parenting is full of paradoxes. Even as we wait for the child to cross her next 
milestone we start missing the precociousness of the earlier stage. As they wean 
themselves away all we can do is gather all the special moments we have had with 
them and air out their warmth every now and then. Finally, if we have given them 
a financially secure future then it is all worth it.<br>
