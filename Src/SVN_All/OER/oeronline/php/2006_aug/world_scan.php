                <p align="justify"><font face="Verdana" size="1"><b><br>
                Jet engine market worth US$600 billion</b><br>
                Rolls-Royce expects a continuing, strong growth throughout Asia 
                Pacific to underpin global demand for 51,000 new commercial 
                aircraft, ranging from business jets to high-capacity airliners, 
                over the next 20 years. The company said that 114,000 jet 
                engines would be required to power the global fleets’ growth 
                with a potential value of US$600 billion. The company believes 
                that high fuel prices will drive airlines to focus even harder 
                on operating cost benefits of highly efficient, new-generation 
                jetliners.<br>
                <br>
                Rolls-Royce expects air traffic demand in the Asia Pacific to 
                grow at an average of 6.6 per cent year-on-year throughout the 
                forecast period. The growth in China over the next 20 years is 
                anticipated to be around eight per cent per annum. Demand from 
                China is expected to total 6,500 engines, valued at US$65 
                billion through to 2026.<br>
                <br>
                India continues to be recognised as a major, emerging force in 
                commercial aviation. India is included within the Asia Pacific 
                forecast total, but is expected to provide a potential market in 
                its own right for at least 1,600 engines in the next two 
                decades.<br>
                <br>
                The North American market, although growing more slowly than 
                other regions, remains the largest market for single-aisle 
                aircraft and business jets. Much of the demand is underpinned by 
                the need to replace existing aircraft, giving a market for 
                around 20,000 engines for mainline and regional aircraft, and 
                30,000 engines for business jets — worth a total of US$160 
                billion.</font></p>
