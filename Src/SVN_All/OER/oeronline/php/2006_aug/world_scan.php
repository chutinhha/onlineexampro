                HP’s new generation tiny wireless chip<br>
                </font></b><font face="Verdana" size="1">
                THewlett–Packard Co. has developed a tiny wireless data chip 
                that can store up to 100 pages of text and could ultimately be 
                used in a variety of consumer and commercial applications. The 
                chip can transfer data at 10 megabits per second, 10 times 
                faster than Bluetooth wireless technology, comparable to Wi-Fi 
                rates and far faster than RFID (radio frequency identification). 
                Also, HP has managed to store up to 4 megabits in working 
                prototypes of the chip, far more than an RFID chip can store. 
                The chips is said to be embedded in paper or stuck to surfaces, 
                and may be sold eventually as self-adhesive dots. It needs no 
                battery or external electronics, getting its power via inductive 
                coupling from the read-write device. Inductive coupling is an 
                energy transfer from one circuit component to another through a 
                shared electromagnetic field. Consumers could store audio 
                commentary, music or short videos on such a chip, affixed to a 
                printed digital photograph. Devices to read and write data on 
                the chip would then eventually be embedded in cell phones, 
                handheld computers, personal computers, printers, or small 
                standalone readers.</font></p>
                <hr color="#8A8A8A" width="80%" size="1">
                <p align="justify"><font face="Verdana" size="1"><b>Advergames hard selling to kids</b><img border="0" src="gcc_dairy_10.jpg" align="right" hspace="5" vspace="2"><br>
                Major companies that sell candy, cookies and sugary cereals have 
                turned to flashy web sites and Internet games to draw children 
                into their snack-food world, according to a study done by US 
                based Kaiser Family Foundation. Kids can ride through the lucky 
                charms ‘Wild Chocolate Mine,’ visit Kellogg’s FunKtown or kick 
                it in the Chips Ahoy! Soccer Shootout, online activities that 
                will keep them immersed in the products longer than they spend 
                watching a typical television spot, the study found. “It still 
                doesn’t have the reach TV advertising has. But who it does 
                reach, it reaches more deeply,” said lead researcher Vicky 
                Rideout. To assess how big these ‘advergames’ are, researchers 
                looked at the 77 web sites of top food companies such as 
                Kellogg’s and Wrigley’s. They found that between June and 
                November of 2005, these sites had more than 12.2 million visits 
                by children.</font></p>
