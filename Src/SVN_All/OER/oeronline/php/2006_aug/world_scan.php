                <p align="justify"><font face="Verdana" size="1"><b>Nortel, Microsoft join forces</b><br>
                Nortel Networks Corporation and Microsoft Corporations have 
                joined hands to start a strategic alliance in order to develop 
                and market products that are supposed to merge telephones and 
                computers in the coming wave of business-communication 
                technology. The deal comes on the heels of an announcement by 
                European telecom giants Nokia Corp. and Siemens AG, that they 
                will merge their network-equipment businesses into a joint 
                venture. Microsoft and Nortel said that, under their deal, they 
                will license some of each other’s intellectual property and 
                deploy the other’s technologies in their corporate networks. The 
                arrangement is currently expected to run for four years, but 
                could be extended. Microsoft Chief Executive Steve Ballmer 
                predicted that “within 10 years, all business communications 
                will be Web-based, and will change the way people communicate.” 
                Ballmer also pledged that five major products will be ready for 
                shipping by July 2007 under the new alliance.</font></p>
