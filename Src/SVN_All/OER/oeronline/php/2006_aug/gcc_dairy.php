<br>
</font><b><font face="Verdana" size="4">The Blooming Oil Boom</font></b><font face="Verdana" size="1"><br>
<b>World Bank report: Economic growth is on a roll since 2003 — but next year 
the flush may fade for some<br>
</b>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">Combined 
with production increases, rising oil prices have fuelled extraordinary economic 
growth among oil producers including the GCC countries, which recorded an 
average economic growth rate of 6.7 per cent over 2005 and also accounted for 84 
per cent of Middle East and North Africa regional growth last year. The World 
Bank report MENA Economic Developments and Prospects 2006: Financial Markets in 
a New Age of Oil finds that oil producers are increasingly turning finite oil 
wealth into longer-term revenue streams.<br>
<br>
Unlike the earlier boom period, when oil producing countries built up debt and 
banked on continued high oil prices to fuel their expansion, and were 
consequently caught with large debts when the oil price declined, this time, 
says the WB report, these countries are showing tremendous amount of restraint — 
building up savings, paying off debts, and setting up oil stabilisation funds.<br>
<br>
Besides some external debt, the majority of debt is domestic for the GCC 
countries. So, a few countries have utilised the oil wealth to substantially 
draw down their domestic debt obligations. Going by the WB report, Saudi Arabia 
has taken the lead in massive debt repayments. In a span of just three years, it 
has brought down domestic debt from 97 per cent of Gross Domestic Production 
(GDP) in 2002 to just 41 per cent by the end of 2005. Over the same period, it 
turned a fiscal deficit of almost 6 per cent of GDP to a fiscal surplus of 8.4 
per cent by 2005. Debt reductions have also occurred in Kuwait and Oman.<br>
<br>
Fiscal situation: A decade ago the situation was entirely different. Most GCC 
countries faced the problem of heavy fiscal deficits because of lower oil prices 
and production, high imports of goods and services, and war-related payments. 
But the trend is slowly but steadily changing.<br>
<br>
The GCC nations, which control nearly 45 per cent of the world oil deposits, are 
also expected to record the largest external fiscal surplus in 2006. The GCC’s 
current account hit a record US$105.7 billion in 2004, and swelled to US$152 
billion in 2005. In 2006, the balance is projected to hit another record of 
US$172.7 billion before easing to around US$164.9 billion in 2007, as per the 
World Bank report. <br>
<br>
Thanks to the sharp growth in the country’s liquefied natural gas exports, Qatar 
recorded the highest current account surplus of around US$45.6 billion in 2005, 
and is expected post the highest surplus of nearly US$51.7 billion in 2006 and 
US$51.8 billion in 2007. Kuwait comes next with a surplus of US$43.3 billion in 
2005, and expected surpluses of US$49.9 billion and US$48.7 billion in 2006 and 
2007 respectively.<br>
<br>
The United Arab Emirates (UAE) is also likely to increase its surplus from US$22 
billion to US$27 billion in 2006, but may slip a little to US$26.1 billion in 
2007. On the other hand, Saudi Arabia’s surplus will remain at the same US$28.3 
billion in 2006, and drop down to US$23.9 billion by 2007, predicts the WB 
report.<br>
<br>
Oman is hitting a high fiscal note — US$7 billion surplus in 2005, projected to 
touch US$8.5 billion this year and US$9.4 billion in 2007. For Bahrain, its 
surplus was as high as US$5.8 billion in 2005 and this year’s accrual is 
targetted to be US$7.3 billion. But 2007 may see a lower surplus — around US$5.3 
billion.<br>
<br>
<b>Growth rates:</b> Economic expansion in GCC countries has been impressive, 
averaging more than 7 per cent over the past three years. Saudi Arabia gained 
more than a percentage point in 2005 in comparison to the 2004 growth rate, but 
has not been able to better the 2003 record — an all-time high in 15 years. For 
Kuwait and Qatar, it’s been a bit of a yo-yo over the last three years, but 
growth rate in the UAE seems to have slowed down somewhat.</font></p>

