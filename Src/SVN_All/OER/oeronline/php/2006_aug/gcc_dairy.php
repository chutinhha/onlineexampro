<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
<br>
The International Monetary Fund’s (IMF) latest report commends that the UAE’s 
medium-term economic outlook remains favourable. Economic growth has been 
impressive — reflecting sharply higher oil prices, increased oil production, 
strong investor confidence, and a significant increase in Foreign Direct 
Investment (FDI). But the report also warned about “...a number of risks, 
including price developments in the equity and real estate markets and 
accelerating inflationary pressures, warrant close monitoring.” It pointed out 
that an inflationary cost-push — given the limitations of monetary policy 
resulting from the fixed exchange rate and liberalised capital account — may 
affect public spending too.<br>
<br>
However, the IMF report agrees that the inflationary fallout is likely to be 
absorbed by the comfortable fiscal position (from oil revenue surplus) which 
allows significant accumulation of financial assets’ support to infrastructure 
and social outlays — leading to an overall growth of the economy. Commending the 
country’s pursuit of market friendly policies, an outward-oriented development 
strategy, and prudent financial policies, the IMF report stressed on the 
following factors for future growth:<br>
• Link wage increases to gains in public sector productivity;<br>
• Replace subsidies on water and electricity with income-related payments to 
nationals below a certain income level;<br>
• Start the groundwork for a Value Added Tax (VAT) system at the federal level.<br>
The IMF report recommended the extension of UAE’s investment climate to areas 
outside the free zones, and take steps to encourage more private sector 
participation by passing the amended Company Law, which will allow higher 
foreign investment in the UAE-based companies.<br>
<br>
On the banking sector, IMF cautioned that increased lending for real estate and 
equity financing has led to a rise in credit risk, and stressed that banks’ 
direct and indirect exposure to these markets — and the increased loan 
concentration in a few large business groups — call for close monitoring by the 
central bank.<br>
<br>
The adequacy of prudential regulation should be kept under review, advised the 
IMF report, and banks that recorded significant growth in profits from equity 
markets related activities should be closely supervised.<br>
