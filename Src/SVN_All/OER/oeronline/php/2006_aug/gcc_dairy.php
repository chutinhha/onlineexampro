The Gulf nations have also been working to strengthen ties with emerging 
economies in Asia, particularly China and India. Most of them have also 
implemented additional tariff reforms (already at relatively low levels), with 
the introduction of the Common External Tariff (CET) among the GCC in 2003.<br>
<br>
With a strong drive to attract business, the Gulf countries are opening up and 
