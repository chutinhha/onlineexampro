<br>
<br>
Firmer oil prices were the primary reason for a record trade balance in fiscal 
2005 — nearly 17 per cent of the GDP — which strengthened export receipts and 
caused trade surplus to double to US$2 billion. While the GDP grew by more than 
7 per cent, oil exports rose by a robust 40 per cent, causing total exports to 
rise by more than 33 per cent — US$ 10 billion — including the output from the 
offshore Abu Saafa field (300,000 barrels per day) shared by Bahrain and Saudi 
Arabia.<br>
<br>
<b>Exports</b><br>
The petroleum sector accounted for nearly 78 per cent of total exports in 2005, 
up from 74 per cent in 2004. Yet, the increasing significance of the petroleum 
sector is regarded as a negative development, as it makes Bahrain’s economy only 
more vulnerable to external shocks. Accordingly, this runs contrary to 
government’s stated goal of diversifying the economy away from oil. <br>
<br>
On a separate note, oil revenue accounted for 73 per cent of Bahrain’s treasury 
income in<img border="0" src="gcc_dairy_3.jpg" align="right" hspace="5" vspace="2" width="150" height="109"> 
fiscal year 2005, again due to firmer oil prices. Yet, the petroleum sector 
(crude plus refining) accounts for 20 per cent of the GDP. Bahrain authorities 
generally refer to the share of the petroleum in the GDP, when arguing about the 
limited role of oil in the economy.<br>
<br>
Not surprisingly, the share of non-oil exports declined from 24 per cent in 2004 
to 20 per cent last year. Non-oil exports (aluminium, ready-made garments and 
chemical products) accounted for 20 per cent of the total exports. Re-export 
accounted for the balance two per cent. The value of re-export merely amounted 
to US$234 million, indicating that Bahrain still has a long way to go to impose 
itself as a base for doing business with regional places.<br>
<br>
<b>Imports</b><img border="0" src="gcc_dairy_5.jpg" align="right" hspace="5" vspace="2" width="150" height="90"><br>
Fiscal 2005 accounted for nearly 22.5 per cent (about US$8 billion) growth in 
imports, mainly on the back of a 50 per cent rise in oil imports. However, 
non-oil imports rose by a mere 3.6 per cent — undoubtedly not commensurate with 
economic growth. Primarily, oil accounted for 52 per cent of total imports in 
2005, up from 43 per cent in 2004. Stronger oil prices increased the 
significance of oil in the import receipt as well.<br>
<br>
Bahrain pays market prices for crude oil purchased from Saudi 
Arabia, and processed in the sole refinery into petroleum products — diesel and 
jet fuel — primarily destined for exports. The Saudi oil is transferred via 
pipelines, thereby saving loading and off-loading costs. However, pipelines are 
several decades old, and are running in areas targetted for urban development. 
The current path of the pipelines could be re-directed, following completion of 
a commissioned study.<i><br>
— Dr Jasim Husain Ali, Director, Economic Research Unit, University of Bahrain</i></font></p>

