<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="1">
While new finds, enchanced oil recovery methods and a better production capacity 
should help boost GDP growth rate in Oman, says the WB report, Kuwait, Qatar, 
Saudi Arabia and the UAE too are likely to show strong GDP projection for 2006 
and 2007. Real term growth too, for these two years, is projected at 7.3 and 5.3 
per cent in Qatar, 7.1 and 6.3 per cent in Bahrain, 6.6 and 5.2 per cent in the 
UAE, 6.3 and 6.4 per cent in Saudi Arabia, 6.2 and 6.0 per cent in Oman and 6.2 
and 4.7 per cent in Kuwait.<br>
<br>
Oil output expansion: As the World Bank report cites, for the third straight 
year since 2003, crude oil prices rose sharply over 2005 — from an average of 
US$38 a barrel over 2004 to more than US$53 over 2005, an increase of more than 
40 per cent year on year. But this has had no effect on demand which has been 
steadily climbing, resulting in shortages. As a result, countries are making 
efforts to increase their output and expand their resources.<br>
<br>
Among GCC countries, Saudi Arabia has increased its output, over the last three 
years, its output from an average of 7.4 to 9.2 million barrels per day (bpd). 
Kuwait too has gone for a strong production drive, with crude production up by 
33 per cent in the last three years. For Qatar, it’s up by 24 per cent, and 23 
per cent for the UAE. Only Bahrain slumped down its oil production by 10-15 per 
cent over 2005 compared to the 2002 output.<br>
<br>
To meet the constant pressure of increasing demand, the GCC nations have been 
investing systematically in explorations and new recovery technologies. Recently 
Kuwait discovered new oil and gas deposits which could boost the country’s 
reserves by some 10 per cent. Saudi Arabia currently produces 11 million bpd of 
heavy crudes — and is planning to produce 12.5 million bpd by 2009, by investing 
heavily in oil field developments.<br>
<br>
Oman, which has heavier crude than its neighbours, has invested heavily in new 
techniques like steam and polymer injection that will boost well production. As 
per Central Bank of Oman Annual Report 2005, Oman’s average daily production 
during 2005 was at about 0.77 million barrels per day. The UAE too has agreed to 
add 200,000 bpd, increasing total production capacity to 2.7 million bpd.<br>
<br>
Widening the scope: High oil prices have jacked up production cost of 
energy-dependent industries like petrochemicals. Globally, the petrochemicals 
industry has grown by four per cent per year over the past two years. For the 
GCC countries, a widening cost advantage has come in handy to bolster their 
petrochemical production facilities. So much so that over the past two years, 90 
per cent or more of the non-oil export growth in Saudi Arabia, Kuwait and Qatar 
has come from petrochemicals.<br>
<br>
The GCC countries now account for nearly 10 per cent of global production in 
basic petrochemicals such as ethylene. By 2010, these countries are expected to 
provide nearly 50 per cent of the world’s annual new ethylene capacity, and 
account for nearly 20 per cent of total global capacity. <br>
<br>
Saudi Arabia’s petrochemical company SABIC has initiated construction on several 
petrochemical plants that will produce ethylene, ethylene glycol, polyethylene, 
and polypropylene products by 2008. The firm has also joined several 
international firms in expanding or building four additional facilities that 
will produce styrene and olefins which will also come online in 2008.<br>
<br>
Other GCC countries too are investing heavily in petrochemical enterprises. Oman 
has announced plans for a petrochemicals complex, including two methanol plants 
that will produce 2 million tonnes by 2008. Kuwait is expanding its 
petrochemicals production with several new facilities that will be online by 
2007. Qatar is finishing a methanol plant this year, and will add two ethylene 
crackers producing 2.7 million tonnes a year by 2011. The UAE has added another 
ethylene cracker that will be running in 2009. <br>
Exports: Oman, Saudi Arabia, the UAE, Kuwait, Qatar and Bahrain more than 
doubled their oil exports in the last three years, growing from a cumulative of 
US$186 billion in 2002 to US$440 billion by 2005.<br>
<br>
Exports of liquid chemicals from the GCC were 16.6 million tonnes in 2004, and 
reached 18.4 million tonnes in 2005. This amount is expected to rise to 32 
million tonnes by 2007, and nearly 48 million tonnes in 2008, as the planned 
petrochemical facilities come on-stream.<br>
<br>
On the future prospectus of the region, the WB report says, hydrocarbon revenues 
are anticipated to remain at quite high levels despite the moderation in oil 
price — easing from US$260 billion in 2005 toward US$225 billion by 2008.<br>
<br>
The WB report concludes that current account surplus is seen to be diminishing 
from about US$185-to US$80 billion, as more of the windfall is expended on 
imports — and the overall fiscal position drops from the current surplus of 21 
per cent of GDP to a still high 15 per cent. <i>— Sowmya Sri Rallabhandi<br>
</i><b><br>
</b><br>
<br>
<br>
<img border="0" src="gcc_dairy_2.jpg" align="left" hspace="5" vspace="2" width="200" height="36"><br>
