High oil prices helped Kuwait’s Gross Domestic Product (GDP) to achieve a growth 
of 35 per cent and totaled US$81 billion in 2005, the National Bank of Kuwait (NBK) 
said. The oil sector recorded a 60 per cent increase. However, growth of non-oil 
sectors has declined — from 17 per cent (2004) to about 10.7 per cent (2005). 
Their contribution to the GDP has also dipped from 50 per cent to 41 per cent. 
However, financial institutions, the leading non-oil sector in the economy in 
terms of contribution to non-oil GDP, recorded a growth of 20 per cent and 
contributed 18.4 per cent of non-oil GDP. But real estate’s contribution to the 
non-oil GDP decreased from 15 per cent in 2002 to 11.7 per cent in 2005.<br>
<br>
While Per Capital Income has gone upto US$34,482 per year, private consumer 
spending has increased by 12.5 per cent — the highest since 1996. But public 
spending, about 4.6 per cent, has remained unchanged for the third consecutive 
year.<br>
