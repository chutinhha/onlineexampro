<br>
With a private sector investment of nearly US$8 billion over the next 10 years, 
Prince AbdulAziz Bin Mousaed Economic City at Hail is set to become the largest 
transportation and logistics hub in the Middle East, and open gates for the 
regional and international investors. Planned by the Saudi Arabian General 
Investment Authority (SAGIA), it is the second in a series of economic cities in 
the country — and is expected to exploit the region’s core strengths of 
location, agriculture, mineral deposits, moderate climate and natural beauty. 
The city will be developed by Rakisa Holdings in cooperation with core investors 
including Gulf Finance House, Abu Dhabi Investment House and Tanmiyat Group.<img border="0" src="gcc_dairy_8.jpg" align="right" hspace="5" vspace="2" width="150" height="104"><br>
Covering an area of over 150 million square metres, Hail eco city will 
incorporate a cluster-based development comprising transportation, logistics and 
supply chain centres; educational services; agricultural and food processing 
services; mining and commerce services; housing; and infrastructure. In the area 
of transportation, logistics and supply chain, an international airport will 
serve 3 million passengers per year, while a railway station will cater for some 
2 million passengers annually. Dry ports and operation centers will be capable 
of handling over 1.5 million tonnes of cargo annually. New highways are also 
being constructed to link the Kingdom through Hail with Jordan, Iraq and also 
Madinab.<br>
<br>
Hail’s sound base in agriculture accounts for 70 per cent of the region’s 
employment in this sector, and 90 per cent of corn, 33 per cent of potatoes, and 
31 per cent of barley produced in Saudi Arabia. Total annual production is in 
