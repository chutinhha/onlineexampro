    <p align="center" style="margin: 10" dir="ltr">
    <b><font face="Verdana" style="font-size: 15pt">An online message for the 
    Gulf media </font></b><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">
    Last year we had raised the subject of how traditional print media 
    companies, especially in the Gulf region, need to constantly adapt and 
    innovate as per the changing market dynamics (“Move out of fortress, take up 
    new battles” — OER December 2005). The success of new media companies 
    including Google, Yahoo, eBay and Amazon has underlined the fact that 
    Internet and mobile phones are increasingly going to be the source for media 
    consumption globally. You might ask, then why are we repeating it when we 
    all know about this media transformation? Fact is, when it comes to our own 
    Gulf region we all believe that new media is still in its infancy, with 
    hardly any viable business to be seen here. How wrong we are! <br>
    <br>
    Last month, AME Info, a leading Dubai based provider of online business 
    information in and about the Middle East region, was acquired by the UK 
    based media giant Emap’s business-to-business division, Emap Communications. 
    AME Info was privately owned, with HSBC Private Equity Middle East as a 
    minority investor. It employs just 20 people at its Dubai Media City office. 
    The acquisition is expected to cost Emap US$29 million. The amount may seem 
    little compared to the hundred million dollar plus acquistions and mergers 
    taking place in other markets. But the AME Info deal is perhaps the 
    beginning of an online era in the region. From now on, those who overlooked 
    online — and considered it as a nonviable business proposition — will have 
    to take the medium seriously. We might see dozens of new generation 
    entrepreneurs, seeded by venture capitalists, mushrooming in the Arab world 
    in the next few years. In the 90s when Sabeer Bhatia sold his brainchild 
    hotmail.com to Microsoft for US$400 million, an entire generation was 
    inspired to kickstart millions of net ventures across the globe. <br>
    <br>
    Quite interestingly, Emap is selling off its consumer magazines in France on 
    one hand; but on the other hand increasing its presence in the online space 
    in the Middle East. In fact Emap’s press release said: “This acquisition is 
    in line with Emap’s aim of targeting faster growth media platforms. It also 
    provides Emap Communications with a broad and diverse platform to develop 
    existing products and to accelerate the pace of new product development in 
    the Middle East.” This, indeed, speaks volumes about the new media’s 
    business potential in the region. The Middle East is one of the fastest 
    growing Internet markets in the world. In the last five years, Internet 
    users have grown by 454.2 per cent compared to 180.6 per cent in rest of the 
    world. <br>
