    The AME Info acquisition also underscores the fact that an enterprise, based 
    on a sound and strategic business model following the best industry 
    practices, is going to succeed despite the small size of the online market 
    in the region. In this market, where there are very few audited print 
    publications, AME Info opted for ABC audit which certified its 752,438 
    unique users (March 2006). The audit certification proved to be a critical 
    factor in the acquisition process, going by a senior Emap official’s 
    confirmation that if the audit had not been in place, it is highly unlikely 
    that they would have done the deal.<br>
    <br>
    The trend is catching on. Zawya, another leading Dubai headquartered Middle 
    East online business information company serving the high-end business and 
    financial professional community, has also got ABC certification for the 
    traffic on its website. The print titles too should take a cue from the 
    online players, and get their circulation claims verified by internationally 
    acclaimed media auditors. Apropos market speculations, a couple of UK and 
    Germany based media companies, frustrated by the stagnant growth in their 
    home markets, are looking to increasing their foothold in the Middle East 
    region using the acquisition route.</font></td>
