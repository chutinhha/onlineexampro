Dam Murphy, Senior Director — Marketing, Bridgestone, is feeling equally 
bullish. “I think the appeal of the LPGA has broadened. We compare it to tennis 
when the Williams sisters (Serena and Venus) brought a lot of attention to their 
sport.” Bridgestone Golf believes that there’s a growing market among women 
golfers. For the swingers of the fair gender, the company has come up with golf 
balls in pastel colours including pink. <br>
<br>
With attractive, talented young women playing with pink balls, the game of golf 
will never be the same!</font></p>
