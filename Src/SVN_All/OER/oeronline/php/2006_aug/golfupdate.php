<br>
<br>
</font><font face="Verdana" style="font-size: 16pt"><b>Fair Prodigies
Hitting the high note<br>
</b>
</font><font face="Verdana" size="1">
<b>Entry of the talented teen brigade is revolutionising the LPGA Tour, with 
more and more sponsors and TV audience getting hooked to the game<br>
<br>
</b>
<img border="1" src="golf_update_1.jpg" align="right" hspace="5" vspace="2"></font><font face="Verdana" style="font-size: 11pt"><b>I</b></font><font face="Verdana" size="1">n the early 1960s, the men’s PGA Tour was 
completely transformed with the entry of young turks like Arnold Palmer and Jack 
Nicklaus, coinciding with the dawn of TV-sports era. Golf was no longer a fringe 
sport with limited appeal, very much becoming a part of the sports mainstream. 
The LPGA Tour is also experiencing a similar trend with the emergence of a young 
crop of LPGA stars, which is fast changing the business of women’s golf. To play 
on the LPGA circuit you have to have at least 18. That’s the rule. But talented 
young golfers below 18 have been allowed to play as special cases.<br>
<br>
Paula Creamer, just 19, is already a champion golfer — with a professional 
victory even before she graduated from high school. Last season, she won US$1.5 
million. Creamer represents six companies including TaylorMade-Adidas Golf and 
Bridgestone, endorsing a wide variety of golf equipment and lifestyle products.
<br>
<br>
The 17-year old Morgan Pressel, the US amateur champion and runner up at the US 
Open 2005, has been making waves by winning at every level of her development. 
She represents Callaway Golf, the leading golf equipment company. <br>
<br>
Michelle Wie, at a tender age of 16, is the new kid on the block. She has taken 
an altogether different route to fame and stardom by often playing against men. 
With a US$10 million Nike endorsement contract in her kitty, Wie is cracking 
aces.<br>
<br>
Besides the three teenyboppers, the LPGA Tour is hotting up with a host of 
talented Koreans, Japanese sensation Ai Miyazato, Sweden’s Louise Stahle, and 
firmly entrenched players such as Cristie Kerr, Natalie Gulbis and Christina Kim 
giving the numero uno Annika Sorenstam, 35, a run for her money.<br>
<br>
Nothing succeeds like success — and LPGA has quickly caught adapted to the 
“catch ‘em young” mantra. The decision, of reducing the minimum age requirement 
for women to join the world’s top women golf tour, is expected by the end of the 
year. Those who are in favour of reducing the age criteria cite the fact that 
the Women’s Tennis Association (WTA) allows players as young as 12 on its tour.
<br>
<br>
Carolyn Bevins, LPGA Tour Commissioner, is quite upbeat with the surge in LPGA’s 
business. “You are going to see us take off at the top of a crescendo this year. 
We are going to exceed everyone’s expectations,” she exults. Bevins has reason 
to glow — the excitement among the fans, sponsors and TV audience is at an 
all-time high. So much so that the LPGA’s TV viewership has shot up by more than 
40 per cent this year. Even last year the ratings were going north. “That’s 
partly due to the success of highly heralded young generation,” acknowledges 
Bevins.</font></p>

