<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10"><b><br>
<font face="Verdana" size="4">Market slumps again</font></b><font face="Verdana" size="1"><br>
<b>After a smart recovery last month, Muscat Securities Market is giving jitters 
to the investors again<br>
</b><br>
</font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he market’s movements this month were very similar to those of most of the 
other GCC markets, with the MSM 30 index having dropped by 4.10 per cent over 
the last 4 weeks. All three sector indices experienced drops in value as well, 
although the services sector is the only sector this month that has reached a 
level higher than at the start of the year.<br>
<br>
<b>Top Gainers and Losers</b><br>
The companies which headed the gainers’ chart were Dhofar Marble and Granite, 
Gulf International Chemicals, Oman Cables Industry, Al Ahlia Detergent and Al 
Fajar Al Alamia.<br>
<br>
The companies that faced the biggest drops in share value this month were Oman 
Aviation Services, Al Sharqiya Investment Holding, Global Financial Investments, 
Financial Services and Alliance Housing Bank.<br>
<br>
The leaders in terms of volumes traded as well as turnover were Renaissance 
Services SAOG and BankMuscat, both of which traded volumes of over RO10 million 
this month. With the completion of the second quarter of 2006, most of the 
companies listed on MSM have by now released their first quarter results (ending 
31 March 2006). After comparing sales for the first quarter of 2006 to sales for 
the first quarter of 2005, we find that petroleum marketing services, brokerage 
firms and unclassified services have shown a marked improvement — with over 30 
per cent higher sales than in Q1 2005.<br>
<br>
As with net profit after tax, key elements of the industry sector — packaging, 
fast moving consumer goods and cement manufacturing — witnessed growths in net 
profit of more than 50 per cent higher than profits achieved in Q1 2006. The 
industry sector can be observed as having a substantial increase in net profits. 
Significant developments have occurred in the sector, with projects of over RO10 
billion already underway.<br>
<br>
This sector has witnessed a significant amount of growth over the past year, 
contributing to the Sultanate’s GDP to a great extent. The end products from the 
various industries have gained the confidence of importers from overseas, hence 
increasing the level of sales in this sector as well. <br>
<br>
