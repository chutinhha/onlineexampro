<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Sixth Sense Man</font></b><font face="Verdana" size="1"><b><br>
Hussain Ahmed Jawad Ghuloom, CEO of Al Waqia Shoes LLC, has come a long way from 
Corniche to the cutting-edge technologies of today<br>
</b><br>
<br>
<img border="0" src="personality_1.jpg" hspace="5" vspace="2" width="150" height="183" align="left"></font><font face="Verdana" style="font-size: 11pt"><b>B</b></font><font face="Verdana" size="1">usiness is all about taking risks. That's the core principle for 36-year-old Hussain Ahmed Jawad Ghuloom, who modestly ascribes his success to simply 
following his father's advice: “Just do what you want.” But there’s more to it 
than that for the boy “from a conservative family growing up on Corniche, 
Muttrah.” Else why should his school principal, meeting him after nearly 20 
years at a marriage ceremony, exclaim: “Hussain, did you manage to pass high 
school?” And true to his school days' reputation of a mischief maker, he 
deadpanned, “Sir, I run a factory in Rusayl Industrial Estate.” That, to the 
same principal who threw him — and his best friend — out of school in his final 
year!<br>
<br>
Of course, the state-of-art footwear factory specialising in military and safety 
boots — Al Waqia means ‘Protection’ in Arabic — did not come easy. Started as a 
turnkey project in 1995 with two partners, it began spiralling into a liability 
within a year. Production was primarily for export — but there was no export 
beyond a lone container to the UK. Plus, technology was changing and their 
production process had become obsolete. “In my business, synergy is creating a 
demand which is acceptable to the customer. And that was not happening.”<br>
<br>
By the year 2000, the writing was on the wall — with a half million Rial 
liability. That was the time when Hussain decided to call a spade a spade. And 
within a year, he became the sole owner of Al Waqia Shoes Company LLC by paying 
off his partners. “Sometimes you call something a sacrifice. But I don't call it 
that. I look at it from a ‘win-win’ situation. I have to create something so 
that I am also wining and the other person is also winning,” says Hussain who 
prefers to be designated Chief Executive Officer (CEO). Quoting Shakespeare, he 
shrugs “What’s in a name!”<br>
<br>
Take the challenge and diversify fast! Hussain’s two older brothers adopted this 
motto way back in 1978. Their father ran a small shop, started in 1956, at 
Muttrah souk — selling turbans, shawls, lungis etc imported from India. But the 
elder brothers diversified into selling watches. From one shop, they have grown 
to 15 outlets all across Oman. The family shop was converted to a watch shop 
last year, “after our father sort of retired.” And by 1980, they had also gone 
into retail trade.<br>
<br>
The challenge to diversify has been inculcated in Hussain too. Returning after a 
four-year stint (1988-92) in the US, grabbing 220 credits to major in two 
subjects — a first class in Business Accounts, and Marketing and Information 
Systems (MIS) &amp; Quantitative Analysis — the prodigal son first went into 
business with his brothers for three years. In 1995, “I told my dad that we 
should diversify our activities. Get into investments overseas and not just 
concentrate on Oman. So we started investing in India, England and Australia. I
<br>
was looking after all those investments.”<br>
<br>
But the itch to change was strong. Some investments, some property and working 
the stock market — but what next? “Something which I could do just by myself, 
without having the hand of the brothers into it or whatever. I was searching for 
opportunities.” That came as the Oman government, since the ’90s, had started 
giving a lot of incentives for new businesses to come up.<br>
<br>
Hussain says the USP was to search for an idea, see what is not really available 
and create that. That's when he came across a friend who gave the idea of safety 
boots. And things have not been the same ever again!<br>
<br>
<b>Makeover Time </b><br>
The self-acclaimed workaholic confesses that he wasn’t a good student — played a 
lot, skipped school. “Very mischievous, really a different person from what I am 
now. And this continued…till the last year of my high school, when I realised 
that something is not really happening which should happen.”<br>
<br>
Friends got talking about who wanted to go to England or America — and become an 
engineer or study computers. Hussain realised his circle was getting broken, and 
everyone had some dream. That's when he decided to pull up his socks to get 
through the high school with good grades.<br>
<br>
‘Set a target and go for it’ has been Hussain’s forte from his early years. 
Started playing table tennis at 10, and became champion at 12 for three 
continuous years (1982-85) — beating youths double and more than his age. 
Football and roller-skating were his other passions, in spite of the dozen odd 
occasions when he broke his shin or elbow. Later came skiing, courtesy a 
girlfriend studying with him in the US. So the average student (50-52 per cent) 
made up his mind, and got 74 per cent as passing out high school grades 
(“Considered to be quite good then.”) Hussain wanted to study Management 
Information Systems (MIS).<br>
<br>
Life changed the moment he landed in America. “I was all alone, that's the first 
thing I felt. Back home, we had three housemaids who took care of all our needs. 
Not even having to get for a glass of water.”<br>
<br>
But in the US, sharing room with two friends who went with him, Hussain had to 
learn everything — from what to buy in a grocery store, how to cook and wash 
clothes.<br>
Attitude too has changed. Music was taboo in his teens — and some forms of jazz, 
rock and rap have remained so. Now he wouldn't mind heading for a discotheque on 
an overseas trip, to unwind with a soft drink or fruit juice.<br>
<br>
<b>Value Addition</b><br>
From making only safety boots (“It was construction boom time.”), Hussain 
decided to go for “shoes with a higher value — for the military”. That was year 
2001. Upgrading the machinery with a meagre RO15,000, Al Waqia Shoes approached 
the Ministry of Defence which asked for some changes. For six months the company 
suffered. But finally the ministry was satisfied — and Al Waqia Shoes became the 
whole-sole supplier from 2002.<br>
<br>
“But from last year we have a competitor, as our rates are a bit higher,” grins 
an unfazed Hussain. In spite of the stiff international competition — mainly 
from Spain, Italy, China and India — the Al Waqia Shoes CEO has reasons to be 
happy. “An American company was selling boots to PDO. So, we structured our 
product and got the PDO contract for the next five years.”<br>
<br>
Business has grown, though margins are thin. Volume in military boots sustains 
Al Waqia Shoes. “That's our bread and butter. We serve the military of Kuwait, 
UAE, Bahrain, Qatar and Oman. None of these places have their own factories. 
Saudi Arabia has one which fulfill their requirement. But they too import — not 
from us so far,” reckons Hussain who spends half of a month traveling and remote 
controlling his operations by laptop and mobile.<br>
<br>
“I travel — from China to Germany, Kazakhistan to Zimbabwe. Buying and selling. 
Mostly from the eastern countries. Also from Italy, Spain and Germany. We export 
to Kazakhistan, Tanzania, Kenya, and the GCC nations as well as to Syria, 
Jordan, Morocco and Lebanon. Now we are eyeing Europe. Sent a small shipment to 
Denmark. Also negotiating with customers in the UK and Germany.” <br>
<br>
Al Waqia Shoes’ setup is not so huge, confirms the CEO, in terms of demand from 
European market. So they have contracted with two companies — one in India and 
the other in Iran. “They lease our brand name and do production in our name. I 
also have my own people sitting in the tanneries in India when we buy uppers. 
That's to maintain the quality and standards that we want adopted.”<br>
<br>
A confident Hussain considers Oman’s Free Trade Agreement with the US as “a 
positive opportunity and not a threat to us.” He sees Oman's economy gaining 
access to some of their markets “in spite of their Quantitative Restrictions (QRs), 
difficult to meet procedures and norms, and a thoroughly complicated process.”
<br>
<br>
For today’s teens aspiring to become entrepreneurs, Hussain says: “Every person 
has got a sixth sense. Always listen to this sixth sense!”
<i>— Abhijit Sinha</i></font></p>

