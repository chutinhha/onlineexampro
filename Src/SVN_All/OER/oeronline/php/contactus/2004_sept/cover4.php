                    </font><font color="#FF0000" face="Verdana"><font size="4">
            TURMOIL!<br>
            Storm in an Oil Barrel<br>
            </font></font><font face="Verdana" size="1"><b>
            <br>
            <font color="#FF0000"><u>$5 TO $50 IN FIVE YEARS. - What’s next for 
            oil?</u></font></b></font></p>
            <p align="justify"><b><font face="Verdana" size="1">Five years ago, pundits were worried that oil was getting too cheap. The $5 
barrel
looked a genuine prospect, heralding a wave of instability in producing
countries from Venezuela to Iran. Now, the graphs are pointing the other way.
Prospects for $50 a barrel came tantalisingly close, and doom-mongers warn a
terrorist outrage could push prices to $80 and beyond. Attempting to predict the
oil market, then, is a dimwit’s game. But since things seem to have reached some
sort of crossroads, it’s at least worth trying out a few possible scenarios for 
the
way prices could develop. OER presents a guide to the three most likely 
outcomes.</font></b></p>
            <p align="justify"><font face="Verdana" size="1">
            <font color="#FF0000"><b>Scenario I:</b> </font><br>
            <b>What goes up, comes down</b> <br>
            A lot many people think we’re in the grip of temporary market 
            madness. The fever of wartime speculation will ease, and what 
            analysts call ‘market fundamentals’ will reassert themselves. 
            There’s is no reason for prices to crash, but they will drift back 
            down – probably below $30 a barrel for Brent crude (currently at 
            more than $42) by, say, the end of 2004. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The impact:</b> 
            Really cheap oil is not a prospect. With oil prices around OPEC’s 
            target price of $28 a barrel, the market will return to the uneasy 
            equilibrium of the past few years. That sort of price is enough to 
            keep producers ticking over, but not enough to encourage major new 
            sources of crude to be brought on stream. With extraction costs in 
            pricey places like the former Soviet Union at $15 a barrel and more, 
            investing billions in ports and pipelines doesn’t make much sense. 
            And that will keep the market tight, with possibly only 2 million 
            b/d of spare capacity, in a global market of 80 million b/d. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The probability:</b> 
            Less likely than many people may want to believe. This scenario 
            depends on at least one major shift in the supply-demand balance – a 
            definitive easing of Middle East tensions; a serious slackening in 
            demand, especially in the US; or a major spurt in output among major 
            producers. None of these looks hugely likely in the near term. Much 
            hope is pinned on the gradual disappearance of the ‘risk premium’, 
            which has raised prices in response to fears of serious supply 
            disruptio</font><font face="Verdana" size="1">n. Until July, this premium was seen as being $4-8 of the
oil price; now, estimates go as high as
$12. But oil has always carried a risk premium,
the result of the fact that a majority
of it tends to be produced in countries
with perceptibly unstable political
environment. And eradicating what appears
to be a speculative bubble will demand
convincing the markets that the threat
of terrorism has been eradicated for good.</font></p>
            <p align="justify"><font face="Verdana" size="1">
            <font color="#FF0000"><b>Scenario II:</b></font> <br>
            <b>Steep but steady
</b><br>
            Prices remain high, and may continue to march higher. Producers do 
            their best to crank up production, but runaway demand combines with 
            that persistent risk premium to ensure that demand outpaces supply. 
            In the very long term, wholly new sources of oil could be mapped, 
            explored and exploited, but only after years of historically high 
            prices. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The impact:</b> 
            A lot of effort has gone into drawing parallels between now and the 
            oil price shock of the late 1970s. Then, high oil prices battered 
            the industrial economies of the developed world, and accelerated a 
            structural shift away from traditional smokestack businesses. This 
            time, things may not be so bad. In real terms, oil prices are not so 
            high: crude would need to double in price to reach the same sort of 
            levels seen in the early 1980s. And today’s economies are less 
            dependent on oil than they were. The US, for example, now produces 
            twice as much economic output per barrel of oil as it did in the 
            1970s. Forecasting firm Global Insight reckons $40 oil would shave 
            only 0.4 percentage points off US economic growth next year, and 
            that the effect would be moderate as time wears on. Not everyone is 
            so sanguine, however. The more fragile economies of Asia certainly 
            have more to lose from long-term high oil prices. But the effects 
            would be balanced by rising output in other areas, if oil producing 
            countries spend their extra<br>
            petrodollars on more lavish imports. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The probability:</b> 
            Given that this scenario depends largely on things staying as they 
            are, it’s the most likely of all. Even if the risk premium 
            moderates, there are many potential disruptions to supply on the 
            horizon. In Venezuela, for instance, once-plentiful production is 
            being hammered by the weakness of state oil firm PDVSA. Nor is it 
            easy for production<br>
            to grow in response to higher prices. It can take up to a year to 
            bring new output on stream even in established patches of 
            production; opening up new areas is the work of decades. On the 
            demand side, there is little hint of change.</font></p>
            <p align="justify"><font face="Verdana" size="1">
            <font color="#FF0000"><b>Scenario III:</b></font><br>
            <b>Up, Up and away</b><br>
            A terrorist attack or war puts a serious crimp in oil supplies. In 
            Saudi, for example, it is reckoned that a really successful attack 
            on the central oil-processing facility at Abqaiq could halve exports 
            for up to a year. All oil producing countries have their 
            bottlenecks, key ports, pipelines or pumping stations whose 
            elimination would cripple the industry. The market result of such an 
            attack is incalculable. But if Saudi exports were hobbled, $80 a<br>
            barrel does not seem unreasonable. </font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The impact:</b> 
            Consumers can live with $40 a barrel; they can probably also come to 
            terms with $50. The question is: what price level would start to 
            make the global economy really hurt. US analysts say the fuss over 
            the $2 gallon of petrol is a storm in a teacup, but that there would 
            be genuine pain if the price was $3. Among developing economies, 
            very high oil prices will have a devastating effect. <br>
            <br>
            Poor countries tend either to be very inefficient oil users, 
            producing little economic output per barrel of oil, or very sparing 
            consumers, because their economies are primarily agrarian – in which 
            case, high prices will prevent them emerging into the next stage of 
            development. Nor would oil producers benefit, if high prices push 
            consumers toward seeking alternatives to oil. At $40 a barrel, oil 
            producers spread some of their wealth by buying in more imports; 
            that trickledown would simply not happen at, say, $80.</font></p>
            <p align="justify"><font face="Verdana" size="1"><b>The probability:</b> 
            Who can tell? Terrorists are known to be targeting Saudi Arabia’s 
            oil facilities, but their chances of pulling off a real spectacular 
            are un-guessable. The Saudi government says there have been five 
            active al-Qaeda cells in the country, but that four have been broken 
            up, and the last is now being dismantled. Nevertheless, a recent 
            study by the Centre for Strategic and International Studies 
            concluded that ‘‘short-term successes have not removed cadres that 
            are well equipped with arms and explosives, and<br>
            past experience indicates that extremists and terrorists will soon 
            change tactics, acquire better intelligence, and become far more 
            sophisticated’’. However clever the pundits may<br>
            sound, the whole thing is pretty much anybody’s guess.</font></p>
