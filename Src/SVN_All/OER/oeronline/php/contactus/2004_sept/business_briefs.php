                    <p align="justify"><font face="Verdana" size="1"><b>PDO strikes oil at
Malaan<br>
                    </b>Petroleum Development Oman (PDO)
has struck oil in the Shuaiba Formation,
which lies in northwest Oman, in the
Malaan area. The Shuaiba Formation contains
the reservoirs of the Yibal field. The
Malaan location was first targeted in the
year 2000 as a potential place for oil reserves.
Malaan-1, the first well drilled there,
could not produce oil at economic flow
rates. However, by studying the seismic
data and with the use of new technology,
oil reserves have been discovered
along the edge of the Malaan structure.
<br>
                    <br>
                    In February 2004, the Malaan-2 well
began to deliver 1,800 barrels per day.
In June, Malaan-3 was successfully
completed and tested, producing 2,600
barrels per day. ‘’The analysis of the

seismic and well data led to a revised
interpretation of the geological setting,
indicating a stratigraphic trap,” said Stephen
Hiebert, senior interpreter at PDO.
<br>
                    <br>
                    A new drilling technology, called
Schlumberger’s new Logging While
Drilling (LWD), was used in the process.
“PDO and Schlumberger created value
for Oman by combining this new technology
and teamwork,” said Mohsin Al
Hadhrami of Schlumberger. “Without the
use of the LWD tool, the successful production
of Malaan-2 would not have been
realised. The tool enabled us to steer the
drill bit so that it was on average 1.8
metres below the top of the reservoir for
a distance of 700 metres. This represents
for us a step-change in ‘geosteering’
horizontal wells through relatively thin
reservoirs. PDO now has good reason to
review other dormant prospects in
northern Oman that have a similar seismic
image as Malaan. We haven’t finished
with the Shuaiba yet,” he added.
</font></p>
