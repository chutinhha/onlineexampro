<p align="justify"><font face="Verdana" size="1"><b>Higher oil prices bode
well for economy: CBO </b><br>
The Central Bank of Oman (CBO) has
pointed put that higher oil prices will help
maintain the overall economic growth
despite the declining trend in oil production,
which may continue in 2004. “The
declining trend in oil production is expected
to continue in 2004. However, its
adverse impacts on gross domestic product
(GDP), exports and revenues will
be more than offset by rising oil prices.
In fact, the domestic economy remained
stable last year despite a fall in oil production,”
said HE Hamood Sangour Al
Zadjali, executive president of the CBO.
According to HE Al Zadjali, fluctuations
in international oil prices and developments
on domestic oil production
will significantly influence the macroeconomic
prospects of Oman for 2004.
“Strong global growth and high oil prices
could, however, entail stronger inflationary
impulses, and the policy responses
of countries to counter this concern could
be manifested in the form of a reversal
in the interest rate cycle,” he added.
