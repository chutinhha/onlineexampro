<p align="justify"><font face="Verdana" size="1"><b>Government revenue
up by 14.8% in H1
</b><br>
The revenues of the Omani government
rose by 14.8 per cent during the first six
month of the year 2004 to RO1,896.4 million
compared to RO1,651.3 million during
the same period in 2003. The Ministry
of National Economy has attributed this
rise to a 9.1 per cent surge in net oil revenues
which stood at RO1,353.4 million
as at the end of June this year compared
to RO1,240.2 million during the same
period in 2003, and a rise in revenues
from liquefied gas and oil condensates
which amounted to RO186.1 million.
<br>
<br>
Natural gas revenues were also up
by 22 per cent during the first six months
of 2004 to RO30.5 million, compared to
RO25 million in the corresponding period
last year. Customs duties revenues
rose by 15.2 per cent to RO28.7 million,
as against RO24.9 million as at the end
of June 2003.
<br>
<br>
Capital revenues declined by 12.8 per
cent to RO4.1 million at the end of June
2004, compared to RO4.7 million in 2003.
Corporate income tax revenues fell by 4
per cent to RO45.5 million, compared to
RO47.4 million at the end of June 2003.
Other revenues dropped by 19.7 per
cent to RO248.1 million, as against
RO309.1 million at the end of June 2003.
<br>
<br>
Government public expenditure rose
by 22.9 per cent during the first six
months of 2004 to RO1,708.4 million,
compared to RO1,390.2 million a year
ago. The rise in the public expenditure
resulted from a 18.2 per cent increase in
current spending which stood at
RO1,259 million, as against RO1,065.5
million at June end last year; and a 39.9
per cent rise in investment expenses
which stood at RO436.3 million, compared
to RO311.9 million a year ago.
<br>
<br>
Support for the private sector rose
by 2.3 per cent to RO13.1 million, compared
to RO12.8 million in H1 2003. The
state general budget saw a surplus of
RO188 million at the end of June 2004,
compared to a surplus of RO261.1 million
during the same period in 2003.</font></p>

