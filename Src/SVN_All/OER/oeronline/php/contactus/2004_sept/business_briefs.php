<p align="justify"><font face="Verdana" size="1"><br>
<b>OPEC may raise
production ceiling
</b><br>
The Organisation of Petroleum Exporting
Countries (OPEC) may increase its
official production limits to meet actual
output. ‘’There is the possibility because
actual OPEC production is around 30
million bpd and we will discuss it in
September,” said OPEC chief HE Purnomo
Yusgiantoro. OPEC ministers are
scheduled to review production policy
in Vienna on September 15.
<br>
<br>
The organisation’s self-imposed output
limits were raised to 26 million barrels
per day (bpd) from August 1 for the
10 members bound by quotas, excluding
Iraq. But actual output has been
running about 2 million bpd over the
ceiling as OPEC tries to stem a relentless
oil price rally this year.
<br>
<br>
Most OPEC members are pumping
close to maximum and only Saudi Arabia
has any significant spare capacity.
OPEC had 1 million to 1.5 million bpd
of spare production capacity that could
be brought on-stream if needed, added
HE Yusgiantoro.<br>
<br>
Iraq, which is struggling to rebuild
its oil industry after the 2003 war and a
series of sabotage attacks this year on its
oil infrastructure, expects exports to
average 1.8 million bpd this month. An
Iraqi oil industry source said that exports
from the southern Basra oil terminal
were currently running at a rate of 2
million bpd.
</font></p>

