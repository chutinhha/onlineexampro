<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Back to 
the future</font></font><font face="Verdana" size="1"><br>
<br>
<b>As a tribute to the 1955-launched Hemi-powered C300, Chrysler will be 
unveiling its all-new 300C Sedan in the Middle East in September this year, 50 
years after the original was launched. OER was exclusively invited as the only 
publication from Oman to attend the Chrysler 300C Driving Event held in Dubai 
last month. Vicky Kapur reports </b><br>
<br>
<img border="0" src="im35.jpg" align="left" hspace="5" width="235" height="134">When Walter P. Chrysler presented the first car bearing his name as a trademark 
to the public at the New York Motor Show on January 5, 1924, he had pulled off a 
major coup: his Chrysler Six, marketed with the model designation B-70 because 
of its top speed of 70 mph (approx. 110 kmph), set new standards in the category 
of mid-sized American cars. What's more, the first Chrysler became a bestseller 
- and the foundation stone for Chrysler Corporation.<br>
<br>
Over and above this, Walter Percy Chrysler reached one of his great personal 
aims with this car - an aim he had been pursuing since 1908. In that year, he 
bought his first car, a Locomobile, while still working as one of the youngest 
top managers in the American railway industry. He disassembled his new 
acquisition in order to analyse its engineering. According to Chrysler's 
biographer, it had been his dream to become active in automotive production from 
the moment he began disassembling the Locomobile. He decided to turn his back on 
railway management and to become a motor manufacturer. The rest, as they say, is 
history.<br>
<br>
However, On August 18, 1940, 15 years before the introduction of the C-300, 
Walter Chrysler died. Chrysler's obituaries noted a lifetime filled with 
extraordinary achievements that had been undergirded by integrity and a personal 
commitment to excellence. While those ideals had been made manifest by the 
automobiles that bore his name, they also served as the benchmarks that years 
later would give rise to the Chrysler 300 'letter cars'.<br>
<br>
Virtually without exception, American automotive enthusiasts who grew up in the 
mid-fifties would recall that memorable moment when they first encountered a 
1955 Chrysler 300 (pictured on next page). Imposing in appearance and capable of 
a performance best described as intimidating to owners of lesser vehicles, the 
C-300, launched in 1955, was an extraordinary automobile. By virtually any 
standard, the 11 versions of Chrysler 300 were motorcars that exceeded all 
expectations and endowed their driver's memory with unforgettable moments of 
exhilarating motoring. <br>
<br>
Today, half a century later, the Chrysler 300C marks the return of the Hemi 
engine, paying homage to the 300 letter-series lineage 50 years ago. The 
Chrysler 300C, which is scheduled for its Middle East debut in September, 
features the 5.7-litre V-8 engine mated to an electronically controlled 
five-speed automatic transmission with AutoStick. <br>
<br>
<b>The milestone car</b><br>
The introduction of the modern Hemi in Chrysler passenger cars marks several 
milestones. This engine offers more power and torque than any Chrysler passenger 
car engine since the legendary 426 Hemi of the 1960s and 1970s. The legendary 
engine design that powered Chrysler's letter series cars in the 1950s has been 
re-engineered and reborn as a modern, high-performance, fuel-efficient and 
durable power plant known as the all-new 5.7-litre Hemi V-8. <br>
<br>
The Chrysler 300C is also the first modern volume production vehicle introduced 
into the Middle East to feature cylinder deactivation. The Chrysler Group's 
Multi-Displacement System (MDS) seamlessly turns off the fuel consumption in 
four cylinders of the 5.7-litre Hemi engine when V-8 power is not needed, thus 
optimising fuel economy when V-8 power is not needed, without sacrificing 
vehicle performance, providing the car with the powerful benefit of the Hemi 
engine with the fuel economy that would be only expected from a smaller, less 
powerful engine. <br>
<br>
And, to top it all, the Chrysler 300C Sedan, one of the most eagerly awaited new 
vehicles from the Chrysler Group, is the first Chrysler vehicle to benefit from 
the merger between Daimler-Benz and Chrysler International. As a result, 
Chrysler engineers were able to utilise Mercedes technology in ride, handling 
and safety. <br>
<br>
With an entirely new shape based on the company's all-new rear-wheel drive 
architecture, a long bonnet, short deck and dramatic profile complete with 
18-inch wheels, DaimlerChrysler expects the all-new Chrysler 300C classic 
proportions to have a stunning presence on the region's roads following its 
September launch. Paying tribute to some of the first letter series cars, the 
Chrysler 300C Sedan is expected to solidify the Chrysler brand's premium 
position in the market. With its new shape and noble proportions, the Chrysler 
300C redefines the classic American automobile.<br>
<br>
&quot;The introduction of the Chrysler 300C marks an important new chapter in the 
evolving history of our brand in the region and is one of the most important 
episodes of our continuing product offensive,&quot; said Bruce Ohms, Sales and 
Marketing Director, Chrysler, Jeep and Dodge, Middle East. &quot;The all-new 300C is 
a great car with an impressive list of standard equipment and excellent value. 
It will play a significant role in further establishing the Chrysler brand in 
the Middle East.&quot; <br>
<br>
Advancements in technology in the last five years enable a large rear-wheel 
drive car to perform with outstanding capability. With Electronic Stability 
Programme (ESP), All-speed Traction Control System (TCS), and advanced Anti-lock 
Brake System (ABS), the 300C reaches new levels of performance for a Chrysler 
vehicle. With 340 hp at 5000 rpm and 387 lb.-ft. of torque at 4000 rpm, the 
Chrysler 300C accelerates from 0 to 100 kmph in 6.4 seconds and has an 
electronically limited top speed of 250 kmph.<br>
<br>
Transferring all this power to the road is Chrysler's new rear-wheel drive 
architecture. Rear-wheel drive offers improved handling with front wheels 
steering and rear wheels driving. With tremendous advances in tyre technology 
and electronic stability control, Chrysler brings back the rear-wheel drive 
passenger sedan.<br>
<br>
<b>The technology</b><br>
Some of the significant technologies enabling the Chrysler Group MDS are the 
speed of electronic controls, the sophistication of the algorithms controlling 
the systems and the use of Electronic Throttle Control (ETC). The Hemi will be 
able to transition from eight cylinders to four in 40 milliseconds (0.040 
seconds). The Hemi engine with MDS has completed more than 10.5 million 
customer-equivalent kilometres through Chrysler Group's development and 
durability testing. <br>
<br>
The Hemi engine (see box) that powers the Chrysler 300C uses aluminium cylinder 
heads with hemispherical combustion chambers for outstanding airflow leading to 
high power and torque. Dual ignition (two spark plugs per cylinder) increases 
peak power and torque, reduces exhaust emissions, increases fuel economy and 
provides a smooth idle. The combustion system has been refined, and the engine 
uses direct-mount accessories for quiet operation. ETC enables several of the 
technologies used to maximise vehicle smoothness, performance, safety and fuel 
economy.<br>
<br>
Safety firsts include one-touch close for windows and sunroof to prevent 
injuries and a special sealant that fills a tyre to 5 mm in case of a puncture 
reducing the probability of an immediate road-side halt for a replacement.<br>
<br>
<b>Size does matter</b><br>
To say that the new 300 has size on its side will be an understatement. 
Stretching 120 inches, the 300C's wheelbase is 7.6 inches longer than an 
E-class's, more than a foot longer than a Toyota Avalon's. Rear-seaters get 40.2 
inches of legroom, about the same as in the space-efficient Avalon but 4.6 
inches more than in the E-class. The 300C's choptop profile is a styling sleight 
of hand. At 58.4 inches, the 300C is actually 1.3 inches taller than the 
bulbous-looking Avalon. <br>
<br>
The compact rear suspension mostly stays out of the trunk, a flat-floored, 
16-cubic-foot closet. Its aluminium lid mounts with compound hinges to swing 
clear of foreheads. The rear seats split 60/40 and fold flat, opening up even 
more cargo space. <br>
<br>
From the front, the Chrysler 300C will not be mistaken for any other automobile 
on the road. The 300C wears the most elegant execution of the Chrysler 'face' 
yet - the grille is tall and bold, inspired by the memorable V-10-powered 
Chrysler Chronos concept car that made its debut at the 1998 North American 
International Auto Show in Detroit. The newly rendered Chrysler wings flow 
seamlessly into the grille header and contain the gold seal of the brand, 
providing an instantly recognisable design cue. <br>
<br>
The headlamp shape supports the new face of Chrysler. Interior components of 
headlamps and tail-lamps have been given the attention of fine jewellery. 
Special attention has been given to the design of the exterior mirrors and door 
handles. The chrome or body-colour mirror head supports the upscale appearance 
objective. The door handles as well are a new 'pull style' design that look 
substantial, feel good in the hand, and add to the overall upscale appearance of 
the car.<br>
<br>
On the Chrysler 300C's boot lid is a badge that carries the name 300C with the 
word 'Hemi' underneath it, letting everyone know that there is something special 
under the bonnet. &quot;The new Chrysler 300C embodies what we call a holistic 
approach to design,&quot; said Ohms. &quot;Every line, every angle and every proportion 
must not only work together inside and out, but provide a visual promise for the 
rest of the automobile, including the driving experience.&quot;<br>
<br>
<b>Inside story</b><br>
The Chrysler 300C's interior has a handcrafted feel and uses distinctive 
material and textures throughout. Optional interior fringe includes a choice of 
Genuine California walnut or patented faux tortoise-shell interior accents. The 
tortoise shell is used on the steering wheel rim, shifter knob, and inside door 
pulls while door handles and other accents are chrome. Evolving the design seen 
on the current Chrysler 300M, a four-gauge instrument cluster with chrome rims 
and a precision appearance are used in the 300C. The centre of the instrument 
panel contains an analog clock, navigation system. <br>
<br>
Benny Oeyen, senior manager (International Product Development) at 
DaimlerChrysler said that the designers were given as much liberty as they 
needed in styling the 300C, asserting that it was as different from the Magnum 
as it needed to be. He noted that the most important part of the vehicle is the 
face, which is very different. The rear is, in person, somewhat reminiscent of 
past Chryslers, most recently the New Yorker (LH version), but before that the 
Dodge Dynasty and even circa-1970 A-bodies. <br>
<br>
Oeyen noted that every aspect of the exterior and interior was carefully thought 
out to work together as a whole and achieve a look of sporty luxury - the 
combination that made the original 300 series so successful. The interior 
spaciousness is highlighted by a two-tone treatment, which work together to give 
the Chrysler 300C a sporty, yet opulent feel inside. The rear seats are split 
60/40, and fold down for added versatility.<br>
<br>
&quot;Chrysler 300C's beauty is more than skin deep,&quot; said Ohms. &quot;We paid a lot of 
attention to the small details, individual appointments and fits, which together 
convey a feeling of richness to both the driver and passengers.&quot; The 
spaciousness of the Chrysler 300C is enhanced by the more upright windshield 
pillars and overall profile of the new exterior design, resulting in a seating 
position that is two-and-a-half inches higher than the current 300M. This design 
enhances a command-of-the-road feel and aids easy ingress/egress. It may help to 
attract some potential SUV buyers, as well - and we suspect that's who the basic 
design proportions are geared to.<br>
<br>
<b>Driving delight</b><br>
The Hemi-powered Chrysler 300C offers athletic and nimble suspension and 
steering characteristics, with the front and rear suspension tuned for greater 
handling performance. A well damped ride, with reduced body roll, gives a 
precise and responsive driving experience. &quot;We designed the suspension to be 
very responsive while delivering superb ride and comfort,&quot; said Ohms. &quot;The front 
and rear suspension are all-new. We used a short- and long-arm design in the 
front and a sophisticated five-bar link, independent rear suspension.&quot;<br>
<br>
OER took the large, luxury-oriented 2005 Chrysler 300C for a 300-km spin - from 
the Royal Mirage in Dubai to the Hatta Fort Hotel to the Sharjah Natural History 
Museum and back. It'll suffice to say that the drive was a very enjoyable one. 
On the handling front, meaty P225/60 R18 tyres and a sport-tuned suspension 
combine to make the 300C feel a good bit smaller than it is. True, there's 
enough body to be noticeable if you're looking for it, but not enough to detract 
from the fun of tossing the car around down our favourite section of mountain 
road.<br>
<br>
In less enthusiastic city motoring, the suspension offers a supple and 
well-controlled ride. The steering on our test car pulled noticeably to the 
right - a complaint that, interestingly enough, we also heard from a couple of 
other journalists who test drove the Chrysler 300C. <br>
<br>
Stopping power was likewise admirable, due largely to the big four-wheel-disc 
brakes that come standard on all 300 models. The Chrysler 300C features dual 
exhaust tips, large performance disc brakes, unique chrome exterior appearance, 
patented tortoise shell interior highlights, power tilt/telescoping steering 
wheel, rain sensing wipers and Boston Acoustics six-speaker 288-watt digital 
amplifier sound system. </font></p>

