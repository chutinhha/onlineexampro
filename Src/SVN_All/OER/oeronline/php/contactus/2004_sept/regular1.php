<p align="justify"><font face="Verdana" size="4" color="#FF0000">Inching up</font><font face="Verdana" size="1"><br>
<br>
<b>Fincorp examines the latest stock movements on the Muscat Securities Market 
as the Index stays range-bound, albeit moving up a notch</b></font></p>

<p align="justify"><font face="Verdana" size="1">The MSM Index has largely 
remained in a narrow range over the period under review (July 18 to August 19, 
2004), with an overall upward movement of 1.1 per cent to close at 3422 levels. 
This was largely led by companies in the industry sector, which went up by 
almost 5 per cent to close at 2807 levels. However, all the three indices are 
still between 2 to 4 per cent away from their 52-week highs. <br>
<br>
The overall advances continued to lead the declines with a ratio of 1.15, with 
41 stocks gaining during the period. However, there was distinct lack of 
activity during the period as had been the case in the past years as majority of 
the traders take their yearly break. Hence the overall volumes showed a marked 
dip of just above the RO2 million. The bid and ask spreads continued to broaden 
during the period, thus signifying lack of liquidity in the markets. <br>
<br>
Large market capitalisation companies remained fairly stable with the exception 
of Oman Cement, which went up by 4.5 per cent to close at RO4.400 levels. Shell 
Oman, the services heavyweight, went down by 5.5 per cent to close at a yearly 
low of RO5.860 on its continued poor performance in the second quarter of 2004. 
Hence, the overall services sector closed in the negative territory during the 
period under review.<br>
<br>
Small market cap based companies continued to attract investors. This included 
Oman Chromite and Al Batinah Dev., based on their investments along with an 
expected better performance in the second half of 2004. Other companies that 
flared up included Dhofar Fisheries (better performance) and Oman Filters and 
Sweets of Oman on speculative bets. Oman &amp; Emirates further reached higher 
heights to cross the RO3 mark on a stupendous good performance in its second 
quarter of 2004 while companies like OIFC and Oryx JIA turned their downside by 
gaining handsomely during the period. <br>
<br>
Regular market players that supported the Indices included Alliance Housing 
Bank. BankMuscat amongst banks while Oman Textiles along with Al Anwar Ceramics 
represented the industry sector. Both the flourmills continued to surge ahead on 
better performance. <br>
<br>
<b>Q2 Results </b><br>
The results for Q2 continued to be positive for the markets. The overall 
profitability for the markets zoomed up by 36 per cent YoY to touch RO55 
million. This was largely contributed by a phenomenal 92 per cent jump in the 
profitability for the services sector to touch RO10 million, which in turn was 
largely due to the contribution from Renaissance Services, whose profits zoomed 
up from RO897,000 to RO7.9 million. Excluding this, the sector growth would have 
been a sedate 9 per cent. <br>
<br>
Similarly, the overall performance for the banking and investment sector also 
continued to be positive with a profit growth of RO26 million, or 22 per cent in 
Q2 on a YoY basis. It also performed well on a QoQ basis with a similar growth, 
largely on better topline performance. <br>
<br>
The star performer continued to be the industrial sector, which improved its 
performance to further new heights in the second quarter. The overall profits 
improved by a phenomenal 58 per cent to touch RO9.2 million as against just 22 
per cent in the Q1 2004, when compared on a YoY basis. This figure is vindicated 
by the fact that the sales grew handsomely by adding another RO11 million on a 
QoQ basis.<br>
<br>
Overall, the profits grew by 36 per cent in the first half of 2004 on the back 
of an improved turnover with the banking and investment sector contributing the 
largest chunk of nearly 50 per cent of the total profits. Likewise, the Index 
companies contributed to nearly 84 per cent of half-yearly profits, which proves 
that the Index is a fairly good indicator of the overall markets. <br>
<br>
On the valuation basis, the overall discounting for the markets is still being 
pegged around 10 times on annualised earnings basis with the Index stock 
naturally commanding a slight premium over the other lesser-known stocks. 
However, the valuation is higher in Industry stocks as compared to banking and 
investments sector while services stocks are the cheapest, even in terms on book 
value basis. On TTM basis, the valuations suggest that the recent performances 
(last 2 quarters) have been better than the earlier ones.<br>
<br>
<b>Outlook </b><br>
The oscillation that investors have noticed during the last month on the markets 
makes it difficult to forecast the short-term performance of the Index. However, 
if one looks back over a period of few years, then it would be very logical to 
deduce that the Index is trying to find its own level as it reaches new heights. 
Hence, we believe that it is a temporary phase before the Index again starts 
nudging greater heights. <br>
<br>
The fundamentals are also there to support it given the past healthy 
performance, along with attractive valuation ratios and dividend yields. The 
increase in the oil prices, which bode well for Oman's economy, and which will 
ultimately lead to better corporate performances. <br>
<br>
An occurrence of an expected increase in interest rates could act as a spoke in 
the wheels of the economy, especially the banking industry, which has been 
enjoying the highest spreads in the GCC region. However, that could largely be 
mitigated by the Al Barka issue expected to hit the markets in November along 
with the BIG Daddy issue of Omantel expected in early 2005. These offerings 
would raise the expectations of the investors and hence the valuations of the 
companies. With Omani companies still regarded as the cheapest of all GCC 
companies, money would slowly but surely flow in. Hence, investors with patience 
could get rewarded in the long term.

