                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Doing Business in East Africa</font><br>
                    <br>
                    <font face="Verdana" size="1"><b>Several major Omani companies are eyeing up investment and export openings in East Africa.
                    Nayl D'Souza looks at the local businesses beginning to tiptoe into the region and those who have already taken the plunge</b></font><p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg48.jpg" align="left" hspace="5" vspace="5" width="212" height="300"></font><font face="Verdana" size="2">Despite many potential pitfalls and a lack of market intelligence, Omani businesses are starting to eye Tanzania, Kenya and Uganda as markets for international expansion.&nbsp;<br>
                    The historical and cultural links between the Sultanate and these East African countries is still very evident today, but economic ties have waned somewhat during the past two decades.&nbsp;<br>
                    However, there are signs that the tide could be about to turn, and Omani companies are devoting more attention to the investment opportunities in this region.&nbsp;<br>
                    "It's going to be a challenge to export to East Africa but I think Oman has a number of strengths and stands a good chance to compete across various sectors," says Ali Al Araimi, a researcher for the Omani Centre for Investment Promotion.<br>
                    "There are a vast number of opportunities to consider in the region."&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>ECONOMIES&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Kenya, Tanzania and Uganda together provide a potential marketplace of over 80 million consumers. The Kenyan economy was an African powerhouse and has been very strong over the last 25 years, with its GDP bigger than Tanzania and Uganda put together.&nbsp;<br>
                    "However, the market has slumped and there are few signs of this economic freefall stopping," says KPMG Kenya's Tax Partner Richard Ndung'u.<br>
                    "Purchasing power has really come down to a level of basic subsistence, a situation that has got gradually worse during the past five years."&nbsp;<br>
                    Kenya recorded an economic decline of -0.3 per cent in fiscal 2000, the worst performance since independence. But it forecasts growth of 1.3 per cent for full-year 2001, and the government has embarked on a privatisation programme.&nbsp;<br>
                    Kenya has about 900 major manufacturers comprising over 200 multi-national companies mainly from the UK, the USA and the EU. Total registered foreign investment is estimated to be in the region of US$1 billion.&nbsp;<br>
                    Tanzania and Uganda are also showing a greater degree of optimism. Tanzania is confidently targeting economic growth of 5.9 per cent in 2001 and 6.2 per cent in 2002, although these forecasts are expected to be trimmed in light of the world economic downturn.<br>
                    Tanzania's inflation rate has been contained at 4.9 per cent and is set to go down to 4.4 per cent by June 2002. On the whole, its tax base is still very small with a tax revenue to GDP ratio of less than 12 per cent, compared with 25 per cent for Kenya.<br>
                    There is a significant South African presence in Tanzania and investment is particularly prevalent in the breweries, which have developed into the biggest in East Africa. South African investment has also poured into the banking industry with large institutions buying into Tanzania's national bank.<br>
                    Uganda has faced difficult times in recent years with the Congolese war and drought impacting on the country's economy quite heavily. Despite this, forecasts are cautiously optimistic. If the non-agricultural economy can continue to perform well, analysts predict that Uganda could sustain GDP growth of around 6.0 per cent in the foreseeable future. The Uganda Stock Exchange received a big boost when all expenses on Initial Public Offerings (IPOs) were made deductible for corporate tax.<br>
                    At present, East African governments remain committed to fiscal and monetary policies aimed at reducing budget deficits, eliminating corruption, speeding up privatisation, implementing tax and other investment incentives and carrying out public sector reforms.<br>
                    <br>
                    </font><b><font face="Verdana" size="1">LABOUR</font></b><font face="Verdana" size="2"><br>
                    <br>
                    A relatively high level of unemployment exists within Tanzania, Uganda and Kenya (20 per cent to 30 per cent in urban areas, according to non-governmental estimates). Kenya's privatisation exercise continues to shift employment from government and agriculture to the private sector.&nbsp;<br>
                    Skilled labour is relatively abundant in Kenya in all categories but it is not a low-cost country and remuneration packages for professionals and senior managers reflect this fact.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>INFRASTRUCTURE<br>
                    </b></font><font face="Verdana" size="2"><br>
                    Power outages are frequent in East Africa and the level of electricity transmission can fluctuate greatly, but the situation is improving. Kenya, Uganda and Tanzania are contemplating the privatisation of the major road networks between the countries. This is part of an effort to attract over US$4 billion in donor and investment funds required to rehabilitate East Africa's roading network.&nbsp;</font></p>
                    <p><font face="Verdana" size="1"><b>TOURISM</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_january_February/Pg50.jpg" align="right" hspace="5" vspace="5" width="225" height="190">Tourism is the region's most important foreign exchange earner. Tanzania's travel industry has adopted a very aggressive marketing campaign recently after a spate of news reports last year highlighted social unrest.&nbsp;<br>
                    Uganda has gone down the road of eco-tourism to differentiate itself from its neighbours, who have successfully sold safari adventure tours.&nbsp;<br>
                    There is also great effort to consolidate Kenya's tourism sector, which has lost major ground due to fierce competition from other African holiday destinations due to the perception of security risks and an incident of civil disorder in 1997.&nbsp;<br>
                    "Complacency and high pricing have taken their toll but in the long run I believe Kenya will have the edge in tourism, especially executive travel that combines tourism and business," says one industry official.</font></p>
                    <p><font face="Verdana" size="1"><b>OPPORTUNITIES FOR INVESTORS</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_january_February/Pg51.jpg" align="left" hspace="5" vspace="5" width="169" height="277">While East Africa is hardly an economic powerhouse, there are pockets of resistance and analysts say opportunities exist for enterprising East African traders, entrepreneurs and industrialists to obtain cheap raw materials from source and form strategic alliances with overseas companies.&nbsp;<br>
                    "There is definitely good potential for Omani companies to develop exports in the region," says Ndung'u.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>EXPORTS</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Ndung'u says many opportunities exist in the retail markets in East Africa.&nbsp;<br>
                    "Omani companies could certainly pursue exports in luxury household goods and consumables such as television sets and video recorders," he says.&nbsp;<br>
                    "In terms of electronic items, if you can offer value for money and two to three years guaranteed service locally, there are great opportunities.&nbsp;<br>
                    "Many gaps exist that are currently being plugged by small local firms."&nbsp;<br>
                    Potential also exists for items like circuit breakers, transformers and switch gears, motors and irrigation pumps, capacitors, resistors, insulation tape, electrical fittings and integrated circuit boards for both the domestic and export markets.&nbsp;<br>
                    After 13 years in business, Oman Water Pumps (OWP) is gearing up to increase its production for buyers abroad and reduce its reliance on local demand.&nbsp;<br>
                    Based in Rusayl and staffed by 60 employees, OWP's plant originally planned to produce only agricultural water pumps, but it has diversified its production to include petrochemical pumps, fire-fighting systems, control switch gear, motors, and generators.<br>
                    "In the past, we've exported only about 5 per cent of our total production but we're aiming to increase this to around 30 to 50 per cent by the end of 2002," says OWP Executive Director Basim Al-Riyami.&nbsp;<br>
                    The firm has a number of tender bids in the pipeline and Al-Riyami says it is initially targeting export growth in Iraq, having already achieved some progress in the UAE, Qatar, Bahrain and Yemen.&nbsp;<br>
                    If successful, East African countries could be the next market the company looks at to further expand its exports, he says.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>AGRICULTURE</b></font><font face="Verdana" size="2"><br>
                    <br>
                    A number of investment opportunities exist in this area. Edible and other oils produced in East Africa, and particularly Kenya, include butter, ghee and margarine as well as sunflower, rapeseed, cottonseed, sesame, coconut and corn oils. However, a large quantity of palm oil is imported and therefore investments to develop substitutes are being promoted.&nbsp;<br>
                    Anthony Dias, business development manager for Oman's Areej Vegetable Oils &amp; Derivatives SAOG, says: "East Africa has a huge palm oil requirement but Areej is at a disadvantage as the local manufacturers seem to be importing directly from Malaysia or Indonesia. There are opportunities in the sunflower oil market as it is a premium product, and we do sell one-off containers to East Africa, but I don't see any significant sales in the next two to three years."<br>
                    The vegetable oil market has so far remained unaffected by the downturn in the global economy. "As a basic necessity, demand for such goods is normally steady," Dias says.<br>
                    Coffee roasting and grinding are carried out in Kenya and Tanzania, and development potential exists in the production of decaffeinated coffee for export.&nbsp;<br>
                    National Tea Company (NTC) LLC - best known for the Mumtaz brand which has about a 30 per cent share of the local tea market - sources its tea leaves from Tanzania, Kenya and Burundi. The company's management is tentatively considering teaming up with a company in Tanzania to export new products that can be further developed for regional markets.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>JOINT VENTURES</b></font><font face="Verdana" size="2"><br>
                    <br>
                    In general, there are no legal limitations on the percentage of foreign ownership, although foreign firms are encouraged to go into joint ventures.<br>
                    Mohammed Ally, a Tanzanian and director of Zanzibar Spices and Herbs Madawa Ltd, has formed an alliance with a US-based pharmaceutical company to sell herbal medicines and mixed spices on the Internet.&nbsp;<br>
                    He says he is a frequent traveller to Oman and is currently on a visit to search for local partners to form another joint venture. He also identifies problems with the packaging of products in East Africa and considers this to be a significant gap in the market that Omani entrepreneurs could fill.&nbsp;<br>
                    Afroz Hadi, sales manager at National Cans &amp; Packaging Industry (NCPI) LLC, says that his company has not yet targeted East Africa.&nbsp;<br>
                    "The reason we haven't tapped the market is because we want to establish a good base in closer markets which do not carry a freight surcharge."&nbsp;<br>
                    At the time of going to press, a war-risk insurance surcharge of US$100 had been slapped on twenty foot equivalent unit (TEU) containers being shipped between East Africa and the Middle East.&nbsp;<br>
                    In the future, Hadi predicts that NCPI could gradually test Kenya and Tanzania for the canning of insect killers and deodorants as well as milk, tea and coffee containers.&nbsp;<br>
                    "We are well-known, well-equipped and have spare capacity to enter this market."&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>TELECOM AND IT SECTOR</b></font><font face="Verdana" size="2"><br>
                    <br>
                    According to the 1997 Telecom Forum Africa Survey, Kenya remains the largest IT market in the East Africa region.&nbsp;<br>
                    The survey estimates East Africa's market investment in IT in 1997 at US$285 million, of which 50 per cent is in Kenya.&nbsp;<br>
                    There has been an explosion in the use of email and Internet, with a number of companies providing access to the Net.&nbsp;<br>
                    The region's telecommunications sector, in contrast, has been relatively slow to develop.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>METAL INDUSTRY</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Kenya has built up a substantial basic metal sector making a variety of downstream products from local and imported steel scrap, steel billets and hot rolled coils. Kenya imports approximately 277,000 metric tonnes of steel billets and wrought iron each year.&nbsp;<br>
                    National Aluminium Products Company (Napco) General Manager Rahul Malhotra says exporting to East Africa is becoming less of a headache than it once was.<br>
                    Napco is currently exporting some small consignments to Kenya and Tanzania and West Africa as well.&nbsp;<br>
                    "We are wetting our feet in this part of Africa to develop business growth and build loyalty with key customers of the future," Malhotra says.<br>
                    A RO2.5 million expansion programme at the company's plant at the Rusayl Industrial Estate will allow it to penetrate other markets besides the Gulf States, the United States, Europe, Singapore, Brunei and India.&nbsp;<br>
                    Last November, the Napco participated in an industrial trade fair in Tanzania in order to "dig for information and fundamentals, like who are the buyers and what is the price".<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>OTHER INTERESTING SECTORS</b></font><font face="Verdana" size="2"><br>
                    <br>
                    Many attractive investment and export opportunities also exist in the production of paper, chemicals, pharmaceuticals and fertilisers.<br>
                    Kenya does not produce sheet glass and imports 16,000 metric tonnes each year. Substantial quantities of ceramic pottery, tiles, sanitary ware (supplied mainly from Iran and Egypt), and insulators are also imported.&nbsp;<br>
                    The mining of minerals such as gold, precious stones and petroleum cannot be ruled out either.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>MIXED VIEWS ABOUT EAST AFRICA'S APPEAL</b></font><font face="Verdana" size="2"><br>
                    <br>
                    A considerable number of local companies spoken to by OER appeared to be having some difficulty establishing business ties within East Africa, or were uncertain about their involvement in the region.&nbsp;<br>
                    After a recent international trade fair in Tanzania, a spokesperson at Al Rahbi Plastics LLC said the firm received no orders and very little response from their participation at the event. On the other hand, Oman Ceramic Co Marketing Manager Sanjeep Sharma says although the same exhibition presented the company with no sales, it did receive many enquiries which at least confirmed a level of interest.<br>
                    A spokesman for a Muscat-based events management company says it is difficult to find good agents in East Africa. "Omanis are willing to do business in this part of the world but they do not trust representatives enough to supply goods before payment, which is what African traders are demanding," he says. "Omani businesses have to make trips up and down to ensure that there are no problems with the supply chain, so many would naturally be questioning whether this is a worthwhile activity to involve themselves in."&nbsp;<br>
                    Still, it seems that Oman is beginning to decide that East Africa's somewhat murky and troubled waters are worth exploring, and firms that have begun to navigate through them are reporting encouraging results.</font></p>
