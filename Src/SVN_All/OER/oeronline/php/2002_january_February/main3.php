                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Banking on Insurance&nbsp;</font><br>
                    <br>
                    <font face="Verdana" size="1"><b>When the new Banking Law 2000 announced that licensed banks could sell insurance products through their branches, it was seen as a move that would lead to the restructuring of the financial services sector. So is "bancassurance" the next big thing for Omani banks? Shilpa Mathai reports</b></font><p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg40.jpg" align="left" hspace="5" vspace="5" width="225" height="283"></font><font face="Verdana" size="2">&quot;Bancassurance, or the business of banks selling insurance products, is the natural progression in the financial services sector," says Sunder George, Deputy CEO at BankMuscat.&nbsp;<br>
                    "Customers today look for a one stop shop for all financial requirements, compelling banks to create financial supermarkets. Though we are the largest bank in Oman with a 35 per cent market share, maintain customer relationships across many products like ATMs, current accounts and savings accounts, for the customer to look on us as the first option for all his requirements, we have to offer insurance products as well. This is the future of the industry."&nbsp;<br>
                    <br>
                    <br>
                    <br>
 BankMuscat is in the process of acquiring a 49 per cent stake in National Life, the life insurance business of the collapsed ONIC Holding. It has been granted a special dispensation from the Central Bank of Oman (CBO) to do so, and the acquisition is a first for the banking and insurance sectors in the Sultanate.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_january_February/pg41.jpg" align="left" hspace="5" vspace="5" width="150" height="175">Most banks in Oman are examining insurance tie-ups, but the present law greatly restricts the scope of their operation.&nbsp;<br>
                    At the moment banks can only offer floor space to a licensed insurer whose own employees have to peddle their products, because the CBO feels that primary banking business should remain a bank's focus for now.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg41a.jpg" align="right" hspace="5" vspace="5" width="150" height="158"> "Allowing banks to deal with insurance products is not a simple issue," says Hamoor Sangour Al Zadjali, Executive President of the CBO.<br>
                    "One should recognise that insurance is a separate business activity and insurance companies have a certain specialised infrastructure in place.&nbsp;<br>
                    <br>
                    "The effects of convergence of banking and insurance activities will have to be considered by different regulators, and a new product or process always takes time to evolve."&nbsp;<br>
                    Most banking regulators are apprehensive that banks may mismanage the insurance part of their business and use customer deposits to pay policyholders, because they are usually adept in managing short-term assets and liabilities such as loans and deposits. Insurance regulators on the other hand are jittery that insurance companies may do the same with the life savings held by them in the form of policyholder funds.&nbsp;<br>
                    <br>
                    The term "bancassurance" has been around for a while - the concept has evolved following the merger of banks and insurance companies. Driving these developments is the belief that cross selling products is a key strategy in marketing, best done by large companies that have the reach and the ability to do so.&nbsp;<br>
                    The three-way merger between Citibank (commercial banking), Salomon Smith Barney (investment banking) and Travellers (insurance) has been the most high profile example of the integration of financial services so far. In Germany, the now aborted merger between Deutsche Bank and Dresdner Bank was driven by Allianz, the Munich based insurer, who wanted to access the branch network of the two banks.&nbsp;<br>
                    Obvious synergies exist between banks and insurance companies, but why are banks interested in retailing a non-credit product?&nbsp;<br>
                    "Because it seems to compliment the slew of products we offer to our clients," says Salaam Shaksy, General Manager at Majan Bank, Muscat. "It is a great way to achieve economies of scope and scale. In a nutshell, you are enhancing both the utilisation of your existing distribution channels and your product offering (through a more comprehensive range of financial products) and it has been proven that the more interfaces a customer has with his bank, the greater his loyalty."&nbsp;<br>
                    As a result of globalisation and intense competition, bank spreads are falling sharply. Banks are thus trying to use their reach and customer base to increase their fee-based income without restricting their funds. This increases a bank's return on assets and overall profitability, without necessarily being exposed to liquidity and interest rate/pricing risk. Insurance is an ideal option for banks that feel they fulfil the three major requirements for a successful insurance business, namely asset management and investment skills, distribution and capital adequacy.&nbsp;<br>
                    "We require insurance policies in order to process certain categories of loan applications like personal loans, which require a life insurance cover," says Oman International Bank's Deputy General Manager, Khalfan Rashid Al Tal'ey.&nbsp;<br>
                    <br>
                    "So if we have a counter offering life insurance products in our premises, this will be extremely convenient for our customers. We can offer them better rates and because of a bank's wide branch network, insurance will be more accessible, especially to citizens in the interior."&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/pg42.jpg" align="left" hspace="5" vspace="5" width="150" height="172">Insurance companies could benefit from an alliance with banks too. The insurance sector in Oman has been suffering from a skewed claims-to-premium ratio of over 100 per cent - the fall of a blue chip company like ONIC is indicative of the malaise.&nbsp;<br>
                    "Oman is a price sensitive market where insurance companies have been competing on rates," says an analyst in Muscat. "As companies mainly depend on stock market investments for profits, the prolonged bearishness of the MSM has upset the apple cart here. Reforms and a shake-out are inevitable."&nbsp;<br>
                    A bank can offer its insurance partner captive business, reducing an insurer's reliance on volatile and high risk segments and doing away with long collection periods and receivables (because of the bank's involvement all payments will be in cash) and broker's charges.&nbsp;<br>
                    Insurers thus have much to gain by cozying up to banks. So is there a slugfest in the market to forge an alliance with banks?&nbsp;<br>
                    "There is a focus on insurance companies that can deliver added value for banks," says Mark Sommerville, Country Manager Oman, Royal Sun Alliance Company. "Banks want to work in partnership with insurance companies who have the capabilities and expertise to offer competitive products. There are opportunities to develop beneficial relations for both parties but the process has been relatively slow here - the expectation was that as soon as the law was changed, products would be rushed out, that is yet to happen."<br>
                    Insurance companies are also interested in getting bank staff to sell insurance products.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_january_February/Pg42a.jpg" align="right" hspace="5" vspace="5" width="175" height="189"> "The correct way in my view," says Sommerville, "is to have the insurance company train bank staff in dispersing policies. It's not a threat to an insurer in any way; to have insurance company employees man bank counters across the country would be a drain on our limited resources and manpower."&nbsp;<br>
                    <br>
                    National Life's Trevor D'Monte is gung-ho about the benefits that will accrue from his company's alliance with BankMuscat.&nbsp;<br>
                    "It is a great channel to increase the company's reach and promulgate life assurance in Oman. Selling individual life insurance policies does not make a difference to our bottom lines; we need to generate volumes. As for the smaller players in the market who are not able to tie-up with a bank, I would tell them to get creative - to look for niche based products they can service or to go in for consolidation via mergers."&nbsp;<br>
                    Smaller players are predictably nervous about taking on the might and muscle of a bank and have expressed their displeasure at the new turn of events. It's not a win-win situation, they say, and cite the inherent disadvantages of bancassurance.&nbsp;<br>
                    An insurer that aligns with a bank will restrict its organic growth while denying the consumer the right to choose - banks will push clients towards their subsidiaries or companies they have an alliance with, and being a part of a package deal, a customer is forced to comply. Service levels may deteriorate and initially there will be an adverse effect on employment and the profitable functioning of some insurance companies.&nbsp;<br>
                    George squashes all such criticism.&nbsp;<br>
                    <br>
                    "The customer will be free to buy insurance from anywhere he wants to," he says. "However, if he is happy with his existing banking relationship, then he has no reason to look elsewhere. There will be advantages if they buy a policy from us, but if our products are not competitive we won't be in the running.&nbsp;<br>
                    "In fact there will advantages in terms of products offered by pegging liabilities and insurance together, like insurance-linked deposit schemes."&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_january_February/Pg432.jpg" align="left" hspace="5" vspace="5" width="148" height="210">Al Zadjali says consumer protection requirements will apply when financial institutions sell insurance in Oman.&nbsp;<br>
                    "The CBO's present requirement is that the staff of insurance agencies and not bank staff shall sell policies so as to protect customers besides avoiding possible risks for banks," he says.&nbsp;<br>
                    "Eventually, when financial institutions are advised to sell insurance products, sufficient knowledge and professional skills will be required of employees handling policies. Due diligence requirements will have to be fulfilled, operations should be transparent and undue related party transactions should be avoided. Sufficient indemnities should be also in place."&nbsp;<br>
                    Shaksy believes some banks are not yet ready to deal with the risk associated with underwriting insurance. But he says: "As long as a bank sticks to its core competencies and resists the temptation of releasing products that it's not adequately prepared to cope with in terms of risk and service quality, I see no major disadvantages to customers."<br>
                    Though bankers are clamouring to own insurance companies, for the time being at least the CBO wants to keep an arms length relationship between the two businesses. BankMuscat's groundbreaking alliance with National Life will be watched with interest.&nbsp;</font></p>
