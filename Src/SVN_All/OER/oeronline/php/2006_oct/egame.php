            <br>
            </font></b><font face="Verdana" style="font-size: 14pt"><b>Mobile 
            gaming: A booming industry<br>
            </b></font><font face="Verdana" size="1"><br>
            <b>With mobile device users projected to reach over 2 billion by the 
            end of 2006, mobile gaming is definitely not a market to be ignored</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>
            <img border="0" src="egame.jpg" width="176" height="220" align="left" hspace="5" vspace="2">“M</b></font><font face="Verdana" size="1">obile 
            gaming is finally beginning to come of age,” claims Mohammed Al 
            Maskari, Director General, Knowledge Oasis Muscat and organiser of 
            eGames 2006 (www.egamesoman.blogspot.com). Advances in handset 
            technology, such as large colour screens, larger memory capacity and 
            compact physical size, in addition to developments in 3G GPRS, EDGE, 
            Java and BREW (*Binary Runtime Environment for Wireless) look set to 
            enable the market to realise the potential of mobile handsets as 
            fully-fledged gaming devices.<br>
            <br>
            Recent reports suggest that the mobile games market grew by 78 per 
            cent in 2005 alone. “With console gaming, customers are obliged to 
            make the commitment to buy a fairly expensive piece of equipment. 
            But the beauty of mobile is that we’ve a global audience that 
            already owns the hardware in the form of their handset,” commented 
            Al Maskari. Global mobile games revenues are expected to reach RO4.3 
            billion (US$11.2 billion) by 2010, and mobile device users are 
            projected to reach over 2 billion by the end of 2006. It’s clear 
            that mobile gaming is definitely not a market to be ignored.<br>
            <b><br>
            It’s different</b><br>
            What makes mobile gaming so different from any other type of console 
            based gaming is the fact that the ‘potential mobile gamer’ already 
            owns the platform on which he/she can play the game. The sheer 
            penetration of game enabled devices — which stands at 40 per cent of 
            the 2 billion devices worldwide today, and set to grow to 97 per 
            cent of all mobile phones sold in 2008 — means that mobile gaming is 
            highly accessible. Compare this to videogames where, over the past 
            25 years, 500 million consoles have been sold — compared to the 600 
            millon mobiles sold just in the past 12 months. The figures are 
            staggering and so are the opportunities. <br>
            <b><br>
            Who’s Playing?</b><br>
            But who’s playing mobile games and how are they playing? Figures 
            reveal that more women than men are playing games on handsets. When 
            you compare this to the typical console player (male; 14-24) there 
            is an obvious contrast. When we look at the behaviour of the average 
            mobile gamer, we find that games are played in short bursts of time 
            — it’s a virtual snack in comparison to the four-course banquet 
            offered by the consoles. <br>
            <br>
            Let’s briefly consider one of the world’s most dynamic gaming 
            markets, Japan. When it comes to mobile gaming, the Japanese are at 
            the bleeding edge. Only a few years ago nearly every 20-something 
            Tokyo commuter spent their entire journey sending e-mails on their 
            phones; now a healthy proportion of them are playing mobile games.<br>
            <br>
            Most of these a re variations on classic board games like Go and 
            Chess, card games and the occasional dialogue choice driven 
            adventure title, a genre much beloved in the Far East. The Japanese 
            mobile casual games market is thriving — and in time we should 
            expect to see the same boom in Oman. However, despite the fact that 
            nearly half all men and women have played a game on their mobile, 
            it’s estimated that only 5 per cent of these people are downloading 
            games — this shows the incredible potential for the industry. Demand 
            is there and people are playing, but as yet the majority haven’t 
            embraced the concept of downloading quality content available. We 
            need to figure out a way to get the folks downloading — an issue 
            that will undoubtedly be addressed at eGames.<br>
            <b><br>
            Growing Fast</b><br>
            The mobile gaming sector is the fastest growing segment of the 
            gaming market. It’s more than evident that the opportunities are 
            there for those who understand the dynamics of this growing 
            industry. The barriers to entry are low. But to succeed, industry 
            stakeholders require the knowledge and recognition that this isn’t 
            the console industry — and that the end user demands a very 
            different gaming experience in terms of access and playability. <br>
            <br>
            Understanding and acceptance of these factors will ensure that telco 
            operators, content providers and mobile manufacturers continue to 
            drive this burgeoning industry on and up, and its true potential is 
            realised.<br>
            <br>
            *Created by Qualcomm (debut Jan 2001) for mobile phones, BREW is a 
            software platform that can download and run small programs for 
            playing games, sending messages, sharing photos etc. The main 
            advantage of BREW platform is that the application developers can 
            easily port their applications between all the Qualcomm ASICs.</font></p>
