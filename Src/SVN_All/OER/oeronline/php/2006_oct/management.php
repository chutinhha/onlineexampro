<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<br>
</b><font face="Verdana" style="font-size: 14pt"><b>Trading on the trust</b></font><font face="Verdana" size="1"><b><br>
Would you like to collaborate with a person who sells your information by taking 
data off one of your business cards or from the signature attached to an e-mail 
you send? <br>
<br>
By David Batstone</b><br>
<b><br>
</font><font face="Verdana" style="font-size: 11pt">
<img border="0" src="manag.jpg" width="100" height="150" align="left" hspace="5" vspace="2">H</font><font face="Verdana" size="1">ow 
private do you expect your personal contact information to be?</font></b><font face="Verdana" size="1"><br>
Consider the following scenario: A mutual friend tells us that we should get 
together for lunch because we share an interest in, say, corporate training. We 
have a great conversation, and at the end of lunch exchange business cards with 
the plans to stay in touch. I return to my office and immediately sell your 
contact information to a website that gets combed over by sales and marketing 
professionals. Do you feel betrayed?<br>
<br>
One of my favourite business writers over at the San Francisco Chronicle, Dan 
Fost, called me recently to get my take on the company that rewards people to do 
that very thing. You can read his more exhaustive story at sfgate.com. But 
here’s the short take.<br>
<br>
Jigsaw Data Corp is an online marketplace where individuals can obtain fresh and 
accurate business contact information. Members of Jigsaw are invited to provide 
business contacts, and then Jigsaw assembles them for the benefit of its 
community.<br>
<br>
To become a member people have two choices: Pay US$25 a month or add 25 contacts 
per month. Adding contacts, correcting their information, or referring to others 
gives you points that later you can use to purchase any contacts from Jigsaw’s 
database. Members also have the option of selling the points for money.<br>
<br>
Jigsaw’s goal is to map every business organization on the planet, contact by 
contact, and keep them current through a collaborative effort. They claim that 
its resulting database will help business people perform their jobs more 
efficiently and strategically. Jigsaw currently has 105,000 members, and only 
131 of them sell points. Almost all trade data to get data.<br>
<br>
I sure don’t want to collaborate with a person that betrayed somebody else to 
get my information. Moreover, if someone takes data off one of my business cards 
or from the signature attached to an e-mail I send — two common methods that 
Jigsaw encourages — then I would feel like there had been a real violation of 
trust.<br>
<br>
Even though Jigsaw stands on weak ethical ground, the company is thriving and 
raking in investment dollars. It has received US$18 million from venture capital 
firms El Dorado Ventures, Norwest Venture Partners and Austin Ventures; has a 
database of 3 million contacts at 150,000 companies; and the company expects to 
grow to 5 million contacts by the end of the year. This interest in Jigsaw has a 
lot to do with the fact that the market for business information totals US$3.5 
billion — according to Outsell Inc. a Burlingame research firm that analyses the 
industry.</font><font face="Verdana" style="font-size: 11pt"><b><img border="0" src="manag1.jpg" width="300" height="311" align="right" hspace="5" vspace="2"></b></font><font face="Verdana" size="1"><br>
<br>
The activities that Jigsaw encourages to its constituency makes me question if 
the trust that we expect from one another is an implicit agreement or fictional. 
Call it courtesy or unspoken rules, but not every relationship exchange should 
require a written contract.<br>
Jigsaw claims that individuals should have no expectation of privacy when it 
comes to their business cards. But why not? If I want to be contacted to do 
business, then I will put my own information on a website. I also reserve the 
right to give my business card to those whom I choose.<br>
<br>
I do not even view Jigsaw as a great business tool. Maybe it’s more accurate to 
say that it has potential for those individuals who do not have good 
connections. It’s a nightmare for senior managers of big companies. For 
instance, I can imagine anti-globalisation activists could make very good use of 
the site.<br>
<br>
If I decide to give an individual my contact data, it’s not the same thing as 
broadcasting it to the entire world. In a Jigsaw world, we are going to start 
putting a copyright or a privacy statement on our business cards: “I explicitly 
copyright this information.” We very well may head in that direction soon.<br>
<br>
David Batstone is the author of Saving the Corporate Soul and teaches business 
ethics at University of San Francisco, United States.</font></p>

