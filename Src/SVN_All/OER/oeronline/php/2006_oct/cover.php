            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Ever since Oman formulated rules on land ownership early this year — 
            through Royal Decree 12/2006 — the real estate scene in the 
            Sultanate seems to have received a real fillip. Omani freehold is 
            making waves and the fundamentals of a real estate boom are all 
            there to see. Mehre Alam does a realty check.</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>W</b></font><font face="Verdana" size="1">ill 
            freehold define the face of Omani real estate? Will it give the 
            Sultanate an edge over some of its neighbours? As The Wave takes the 
            first step into this uncharted territory, no doubt many are watching 
            with anticipation.<br>
            <br>
            “The demand for our property is high and we are short on supply at 
            the moment,” says Wael Al Lawati, Deputy CEO of The Wave Muscat, the 
            beachfront property owners. The Omani freehold is hot property 
            currently — nationals and expatriates are both lapping it up. This 
            signifies a new era in property investment in Oman. A joint venture 
            comprising Oman’s Waterfront Investments, which includes the 
            Government of the Sultanate of Oman, the National Investment Funds 
            Company and the UAE’s Majid Al Futtaim Investments, The Wave is the 
            first major project launched in Oman to offer freehold status to all 
            purchasers: Omanis, GCC nationals and expatriates.<br>
            <br>
            Consider this: The 221 units in Phase I of The Wave, the $805 
            million integrated waterfront, have all been snapped up. Phase II of 
            200 units of town houses and villas will be released soon. The 
            project has been oversubscribed 20 times for the first release. And 
            adding momentum to the surge, The Wave has joined hands with 
            Alliance Housing Bank (AHB), to provide a range of mortgage 
            solutions to both Omanis as well as foreigners. (There are two major 
            mortgage loans specialised banks in Oman, one, the private-owned AHB 
            and the other, government-owned Oman Housing Bank, OHB.)<br>
            <br>
            This is the first agreement of its kind that allows foreigners to 
            buy their own property in the Sultanate. While Omani nationals can 
            receive a mortgage of up to 90 per cent over a maximum of 25 years, 
            GCC nationals can get finance up to 70 per cent over the same 
            period. Foreigners, including expatriates living in Oman, can apply 
            for a mortgage of up to 60 per cent over a period of 15 years.<br>
            <br>
            During conversation with some other developers, Oman Economic Review 
            (OER) came to know that more alliances of The Wave-AHB kind could be 
            on the cards — to offer mortgage solutions to buyers, including to 
            expatriates.<br>
            <br>
            The CEO of a leading developer specifically hinted about sustained 
            efforts towards chalking out a mortgage agreement with a leading 
            bank.<br>
            <br>
            It was earlier this year, when Oman formulated the new rules on land 
            ownership, through Royal Decree 12/2006, (allowing foreign nationals 
            to buy property in select, designated areas) that the landscape was 
            changed forever.<br>
            <br>
            Asif Parpia, an Indian architect, became the first foreigner to buy 
            a property at The Wave, Muscat’s first residential release — 
            encompassing 221 townhouses and villas. He was among the aspirants 
            who were offered a chance to buy property through a supervised draw 
            held earlier this year.<br>
            <br>
            An architect by profession operating in Oman, Dubai and Mumbai, 
            Parpia has been a resident of Oman on and off since 1982, and 
            harboured the wish to buy property in Muscat. He had kept a close 
            vigil, over the Internet, on the developments. Following the Royal 
            Decree allowing expatriates to own freehold property in the country, 
            he applied at The Wave Muscat during one of his visits to the 
            Sultanate.<br>
            <br>
            Expressing his happiness over being the first foreigner to buy 
            property in Oman, Parpia had said: “The issuance of Royal Decree 
            12/2006 facilitated my decision. I never knew I was the first, but 
            all I can say is I am looking forward to be a part of the 
            community.”<br>
            Oman, already witnessing an all-round boom in its real estate, may 
            well be on the threshold of creating a new yardstick for property 
            development in the region — the freehold way. <br>
            <b> <br>
            Boost To Image</b><br>
            “It’s really heartening to attend conferences abroad where Oman is 
            cited as an example for its bold step on the freehold law,” asserts 
            Al Lawati. Many countries in the region have been talking about it 
            but have not gone ahead wholeheartedly when it comes to freehold, 
            adds the Deputy CEO of The Wave Muscat. <br>
            <br>
            He says that in the case of Dubai, despite its great track record of 
            delivering products on time, people are still not certain about the 
            freehold situation. As for the Omani freehold, he claims to have 
            predicted that it was on the anvil nearly a year prior to the 
            announcement. Most of the to the potential investors had scoffed at 
            it, he recalls.<br>
            <br>
            But it was Al Lawati who had the last laugh. “It’s given me an 
            opportunity to tell those people: ‘Didn’t I tell you so?’”<br>
            <br>
            Ali Hassan Moosa, Deputy General Manager, Alliance Housing Bank, 
            Oman, (see interview) puts it succinctly: “The real estate sector 
            with regard to foreign ownership is much more structured in Oman. 
            While we have freehold, some neighbouring countries offer lease. But 
            there’s a big difference between freehold and lease. A foreigner 
            buying property in Oman will get the title deed. But buying property 
            in some of the neighbouring countries, all that a foreigner will get 
            is a purchase agreement. This shows how advanced Oman is in terms of 
            legislative infrastructure.”<br>
            <br>
            Al Lawati recalls some of the earlier hiccups with regard to the 
            then ambiguity on freehold: “We had held back our sale for some time 
            till the Royal Decree was announced. We didn’t want sell our 
            property on mere assurances and promises, though we knew the law was 
            coming. We did the more astute thing — to wait — so that there was 
            no doubt in anyone’s mind.”<br>
            <br>
            The Wave Deputy CEO agrees that there was a trust barrier to be 
            overcome. “The buyers are coming to invest in a new country. 
            Moreover, they are buying property in a new development. This was 
            the first release of the project, and there is nothing to see on the 
            ground yet. We wanted to minimise the barriers in the people’s mind 
            before getting them to the point of buying.” <br>
            <br>
            Harking back to the early stage, Al Lawati says the law on freehold 
            was a promise that the government had made to The Wave, being the 
            first project, and to the others too. And the promise was fulfilled. 
            “It’s good to know that the legislation required to make these 
            projects stand on firm ground was promulgated well in time.”<br>
            <b><br>
            The Impact</b><br>
            So, what exactly has the freehold done? “To use a cliché, the 
            freehold law has expanded the pie,” declares Al Lawati. He adds: 
            “Were these projects (like The Wave, Blue City, The Muscat Golf 
            Course — all mega projects in tourist designated zones) launched 
            without freehold, we’d have had to rely on a small pool of Omani or 
            GCC nationalities in buying property. Freehold basically means the 
            world is our oyster; we can sell property to people of any 
            nationality around the world. It means that we can now target people 
            who have traditionally not looked at Oman as a residential 
            destination, primarily because it was not an option.” <br>
            <br>
            So what is freehold going to do? One, it is expected to have a 
            multiplier effect on the economy. Says Al Lawati: “I think freehold 
            is going to have a huge impact. It’ll change the market. A lot of 
            people, now living in rented houses on company allowances, may opt 
            to invest in property and capitalise on the allowances instead of 
            paying rent.”<br>
            <br>
            The other impact will be on the quality of construction itself. “It 
            will surely raise the quality of construction. Buyers from different 
            parts of the world will certainly have higher expectations,” says Al 
            Lawati.</font></p>
