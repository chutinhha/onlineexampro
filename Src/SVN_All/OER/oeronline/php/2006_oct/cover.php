                  <p align="center"><font face="Verdana" size="2"><br>
                  <b>It’s all about fundamentals </b></font></p>
                  <p align="justify" style="margin-left: 10px; margin-right: 10px; margin-top: 1px; margin-bottom: 10px">
                  <font face="Verdana" size="1"><b>The real estate boom in Oman is fuelled by very strong and stable 
            parameters, Ali Hassan Moosa, Deputy General Manager of Alliance 
            Housing Bank, tells Mehre Alam</b><br>
                  <b>
            <br>
                  </b>
                  <img border="0" src="cover2.jpg" width="150" height="169" align="left" hspace="5" vspace="2"><b>Is Oman in the midst of a definitive real estate boom?</b><br>
            The answer is yes. But this boom related not related to any one 
            sector alone. The economy as a whole is booming. The real estate 
            sector is an important constituent of the economy.<br>
                  <b><br>
            Has there been an immediate impact of the Central Bank of Oman 
            (CBO’s) move to de-regulate mortgage finance in the Sultanate?</b><br>
            Absolutely! Though Alliance Bank in Oman has been working in a niche 
            and comfortable market, competition is always healthy. Now, 
            commercial banks with their large distribution networks have entered 
            this sector. To add to that, there is much greater awareness on the 
            issue now.<br>
                  <br>
            Earlier, the Omani population used to rely on consumer lending for 
            direct purposes — to buy a car, to educate their children, to go on 
            holiday, to get married, or to build a house. It was an unhealthy 
            situation. With the commercial banks entering mortgage finance, even 
            niche players such as Alliance are facing competition. The demand is 
            likely to shoot up further.<b><br>
                  <br>
            Demand-wise, which segments of housing are more sought after in the 
            market?</b><br>
            In terms of market segments, we cater to a wide spectrum of market, 
            but the predominant segment is the middle-aged professionals working 
            in government, quasi-government and private sector. This group would 
            like to either re-finance its existing consumer loans because it 
            didn’t have the choice, or it has taken consumer loans to build 
            houses and now has the option to switch over to something more 
            comfortable.<br>
                  <br>
            But the chunk of the demand is coming from the younger population. 
            One of the crucial elements of demography in Oman is the change in 
            family trends. Like elsewhere in the world, there is shift from the 
            joint family to the nuclear family system, especially in the urban 
            areas like Muscat and Sohar. So, there is the young married couple 
            that does not wish to live in the family house and is looking 
            forward to establishing its own house. This comprises another 
            segment.<br>
                  <br>
            One of the interesting elements in the fundamentals of the growth of 
            the real estate sector recently is that the government has been 
            actively distributing land. As such, the government was always 
            distributing land — but if you look at the figures, the number of 
            plots allotted or distributed to Omani individuals has tripled in 
            one year. Now, this itself fuels growth of the real estate because 
            these individuals will ultimately require finance to build their 
            houses.<br>
                  <b><br>
            Is this real estate boom more Muscat-centric?</b><br>
            Currently, 50-55 per cent of our moving businesses (Alliance) are 
            underwritten in the Capital area through our two main branches, in 
            Mutaiyah and Seeb. But the rest of the business comes through our 
            other branches. Sohar is growing very rapidly and is becoming a 
            major hub. Rentals in Sohar are even higher than in Muscat because 
            there is too much demand and too little supply. Salalah is growing; 
            Sur is growing. We have a branch in Ibri that is doing extremely 
            well. We have opened a service centre in Buraimi, where in one month 
            alone we have underwritten a few mortgages.<br>
                  <br>
            This clearly shows that the real estate boom is spread across the 
            Sultanate. But obviously, the demand and the rate of growth in the 
            Capital area will be more because of the bigger population and 
            higher income here.<br>
                  <b><br>
            What has been the rate of growth for your bank in the last one year?
                  </b>
            <br>
            In 2005, the loan book grew by 29 per cent, and the total assets 
            grew by 24 per cent. This shows a very healthy growth. As for this 
            year, the first six months results of 2006 again show very healthy 
            results.<br>
                  <b><br>
            Is this real estate boom here to stay?</b><br>
            In the medium term, absolutely yes! The reason for this is that the 
            fundamental of the boom centres round the oil-based economy, 
            although the economy is diversifying towards hydrocarbon, gas and 
            other industries. The contribution of tourism to the country’s GDP 
            is very little at this stage, but it is going to grow after all 
            these mega tourism projects are implemented. The demand for oil in 
            the next five years will continue to grow. The oil prices are 
            forecast to keep climbing up.<br>
                  <b><br>
            Where does the real estate boom in Oman stand when compared to 
            places like Dubai or Qatar?</b><br>
            All I can say is that the real estate sector, especially for foreign 
            ownership, is much more structured in Oman. The Wave, I understand, 
            is freehold. Some other neighbouring countries offer lease. There’s 
            a big difference between freehold and lease. While a foreigner 
            buying property in Oman will get a title deed, all that a foreigner 
            gets, on buying property in some of the neighbouring country, is a 
            purchase agreement. This shows how advanced we are in terms of 
            legislative infrastructure.<br>
                  <br>
            Then, Oman’s landscape is extremely beautiful, and counts as one of 
            the major factors in the Sultanate’s favour. It provides Oman a 
            competitive edge.<br>
            In a nutshell, the major plus points for Oman’s real estate boom are 
            the landscape, lifestyle, security, political stability and the 
            regulations on ownership.<br>
                  <b><br>
            What has been the impact of the announcement of the Royal Decree 
            12/2006?</b><br>
            We have inked a deal with The Wave, the first of its kind, which now 
            provides a range of mortgage solutions to both Omanis and 
            expatriates. This deal is not to fund The Wave project but to fund 
            individuals who wish to purchase property at development. This deal 
            itself is available for Omanis, GCC nationals, expatriates working 
            in Oman and expatriates working elsewhere. We offer competitive 
            rates — up to 15-year loan tenure for expatriates and 25 years for 
            Omanis.</font></td>
