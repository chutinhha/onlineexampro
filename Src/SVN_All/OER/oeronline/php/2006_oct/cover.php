                  <p align="justify" style="margin: 10px">
                  <font face="Verdana" size="1"><b>What The Law Provides</b></font></p>
                  <ul>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">True freehold ownership of land by non-Omanis.</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Clear and documented policy of land ownership and residency rights.<br>Solid legal framework for real estate and tourist market.</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Allows for inheritance to be dealt with in accordance with the laws 
            of the jurisdiction of the owner.</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Grace period for claiming of inheritance (Only after the expiry of 
            15 years the property falls back, if unclaimed).</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Expatriates and foreign companies will be able to mortgage their 
            land in accordance with the laws of Oman.</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Gives banks greater certainty to give loans to foreign property 
            buyers.</font></li>
                  </ul>
                  <p align="justify" style="margin: 10px"><b>
                  <font face="Verdana" size="1">What The Law Is Yet To Provide</font></b></p>
                  <ul>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Exact terms and conditions for the grant of residency permit (yet to 
            be dealt within the implementing regulations, which are yet to be 
            promulgated).</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Procedure for issuing residency permits (This feature of the new law 
            will need to be clarified and detailed by further regulations).</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">Additional conditions to be met by applicants in order to 
            successfully obtain residency.</font></li>
                  </ul>
                  <p align="justify" style="margin: 10px"><b>
                  <font face="Verdana" size="1">What Is Likely To Happen Soon
                  </font></b></p>
                  <ul>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">A committee to be set up to which applications for residency to be 
            submitted. This panel to include a member of Royal Oman Police who 
            will be responsible for handling all residency applications.</font></li>
                    <li>
                    <p align="justify" style="margin: 10px">
                    <font face="Verdana" size="1">More projects/developers likely to tie up with leading banks to 
            offer mortgage solutions to buyers, including expatriates (on the 
            lines of The Wave-Alliance Housing Bank tie-up.</font></li>
