<b><br>
</b></font><b><font face="Verdana" style="font-size: 14pt">Oil and gold: A bear 
market?</font></b><font face="Verdana" size="1"><br>
<b>
<br>
Oil prices have fallen continuously from their July 11 high of $78 to $62 in 
late September. The EU–Iran diplomatic rapprochement has taken out some 
geopolitical risk from the oil market, which has priced in a scenario that 
averts a US military strike on Iran’s nuclear installations, any threat to Gulf 
tanker shipping and loss of Iranian crude exports in the global markets. </b></font></p>
<p align="justify" dir="ltr" style="margin-right: 10px">
<font face="Verdana" style="font-size: 8pt">
<b>By Matein Khalid</b></font><font face="Verdana" size="1"><br>
<br>
<b>
<img border="0" src="pirscope.jpg" align="left" hspace="5" vspace="2"></b></font><b><font face="Verdana" style="font-size: 11pt">T</font></b><font face="Verdana" size="1">he 
hurricane season in the Atlantic Ocean and the Gulf of Mexico has been nowhere 
as catastrophic as last year when Hurricane Katrina devastated New Orleans, 
closed offshore oil and rigs on the Gulf coast and wreaked havoc with 
Louisiana’s natural gas terminals, storage tanks, refineries and pipelines. 
Naturally, natural gas prices tripled on NYMEX in the hurricane panic of 2005, 
though a subsequent mild winter triggered a bear market in natural gas that has 
slashed spot prices by almost 60 per cent since their peak last Christmas. <br>
<br>
The refinery turnaround season, associated with a plunge in gasoline spreads and 
fall in physical demand for crude oil, has also contributed to the 20 per cent 
fall in black gold. Hedge fund liquidation on the leveraged paper daisy chains 
on the New York NYMEX and London IPE futures pits accelerated the softness in 
prices, contributing to a panic sell off in crude oil and natural gas. OPEC and 
the International Energy Agency both cut their estimates on oil demand, putting 
the final nail in the coffins of the oil bulls.<br>
<br>
What next? One, the US is still determined to impose UN Security Council 
sanctions on Iran and the US Treasury has started a worldwide campaign to 
blacklist Iran’s banks from the world financial markets. I believe that the 
energy markets have prematurely concluded that diplomacy will defuse the 
geopolitical US–Iran time bomb. Two, refinery turnaround season is over and the 
seasonal demand for crude oil will only rise after November. Three, the crude 
oil and natural gas prices in the futures markets are now technically oversold. 
Four, Iran is not the only source of geopolitical supply risk in the oil and gas 
market. The Niger Delta secessionist revolt, Venezuela’s Hugo Chavez and 
Bolivia’s Evo Morales’s campaign against the colossi of the Big Oil, the 
sectarian slaughter in Iraq, and the NATO–Taliban war in Afghanistan’s Helmand 
and Kandhar provinces are tangible elements of geopolitical risk even if 
diplomacy defuses the crisis between Qom and Washington. Five, the US housing 
bubble may cause a declaration in consumer spending, the fall in retail gasoline 
will boost, not cut, energy demand. In any case, America’s GDP growth may 
decelerate — but the economic bloc that is BRIC (Brazil, Russia, India, China) 
is the growth engine of the world economy as well as the wild card in the global 
energy supply — demand equation. The obvious conclusion? The risk in oil and gas 
is on the upside, not downside.<br>
<br>
Gold has fallen $160 since its May high of $730 a troy ounce. The Federal 
Reserve pause in credit tightening and the softness in the US housing market, 
has not led to either carnage on Wall Street or a collapse of the dollar in the 
foreign exchange market. Yet, I doubt if gold will fall much below $540–560, its 
summer lows, even if the dollar remains well bid against the Euro and the 
Japanese yen. After all, central bank sales of gold were only 337 tonnes, far 
below the 500 tonnes annual limit of the Washington Agreement. In fact, central 
banks in the Gulf, Southeast Asia, Russia and Latin America have bought gold as 
a diversification from dollar denominated reserves.<br>
<br>
The fall in gold prices can also boost jewellery demand at a time when the Hindu 
Diwali festival/marriage season begins next month. India is the world’s leading 
importer of gold jewellery, and its demand had fallen by half when prices were 
at in the $650–730 range. The same fall in jewellery offtake slashed demand in 
the Turkish and Saudi Arabian gold souks, the two leading bullion 
wholesale/retail markets in the Middle East. <br>
<br>
Gold producer dehedging rose threefold after buying by Barrick Gold when it 
inherited Placer Domes hedge book after the merger deal. Of course, the fall in 
perceived geopolitical risk after the EU-Iran diplomatic meetings also hurt 
gold. The oil-gold linkage, briefly witnessed during the Israeli invasion of 
Lebanon, does not hold on the downsize. As in oil, speculative hedge fund 
selling has amplified the woes of the goldbugs. A fall below $550 would, 
however, see gold fall to as low as $480.<br>
