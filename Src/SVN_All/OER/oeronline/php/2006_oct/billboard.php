        <b>Honda poised to take on the skies</b><br>
        Honda Motor Co., Ltd. has announced that it has established a 
        wholly-owned subsidiary, named Honda Aircraft Company, which will be 
        responsible for the further development, sales promotion and production 
        of the innovative HondaJet. The new company will be based in a facility 
        at the Piedmont Triad International Airport in Greensboro, North 
        Carolina, where the prototype HondaJet was assembled and flight tests 
        have been carried out for more than 3 years. Honda Aircraft Company will 
        begin taking sales orders for HondaJet this fall, with the goal of 
        delivering the first mass production HondaJet in 2010. In addition, the 
        company will develop the production version of HondaJet, pursue and hold 
        both type certification and production certification, and conduct 
        marketing activities for the aircraft. The goal of this alliance is to 
        provide a new level of sales and service to meet the needs of jet 
        customers with the goal of setting a higher standard for the quality of 
        the ownership experience. HondaJet features several innovations that 
        help it achieve far better fuel efficiency, larger cabin and luggage 
        space and higher cruise speed than conventional aircraft in its class.<br>
