    <b><font size="1" face="Verdana">From Biology to Business</font></b><p align="justify" style="margin: 10" dir="ltr">
    <font size="1" face="Verdana"><br>
    </font><font face="Verdana" style="font-size: 11pt"><b>R</b></font><font size="1" face="Verdana">emember 
    the famous observation by the Red Queen to Alice: “In this place, it takes 
    all the running you can do to keep in the same place.” Tongue firmly in 
    cheek, Lewis Carroll was telling his readers — incidentally, literary 
    historians have since ‘discovered’ that Alice in Wonderland was more of a 
    contemporary account of Victorian England’s mode of “doing business” — that 
    in a competitive world, relative progress (running) is necessary just for 
    maintenance (staying put). A hundred years later, Carroll’s parable was 
    conceptualised as the ‘Red Queen Principle’ by evolutionary biologist Leigh 
    van Valen in her book (A New Evolutionary Law, Evolutionary Theory 1, 1973).<br>
    <br>
    What van Valen had done was to extend Charles Darwin’s ‘Survival of the 
    Fittest’ model. Her ‘Red Queen Principle’ says: “Since every improvement in 
    one species will lead to a selective advantage for that species, variation 
    will normally continuously lead to increases in fitness in one species or 
    another.” However, since in general different species are co-evolving, 
    improvement in one species implies that it will get a competitive advantage 
    on the other species, and thus be able to capture a larger share of the 
    resources available to all. The only way that a species involved in a 
    competition can maintain its fitness relative to the others is by improving 
    its design. <br>
    <br>
    Macro-economists, management analysts and human resource managers have been 
    quick to latch on to the vast potentials unleashed by the ‘Red Queen 
    Principle’ and create analogous models — where the most obvious example of 
    this effect is the “survival race” between the predator and prey. The only 
    way predators can compensate for a better defense by the prey (rabbits 
    running faster) is by developing a better offense (obviously, foxes will 
    have to run even faster). In this case, we might consider the relative 
    improvements (running faster) to be an absolute improvement in fitness — and 
    consequently, survive in the long run.<br>
    <br>
    In a sense, the ‘Red Queen Principle’ is a negation of the generalised 
    ‘Peter Principle’, which states that systems tend to evolve up to the limit 
    of their adaptive competence. The ‘Peter Principle’ was first introduced by 
    Lawrence J. Peter in a humoristic book (of the same title) describing the 
    pitfalls of bureaucratic organisations. The original principle states that 
    in a hierarchically structured administration, people tend to be promoted up 
    to their “level of incompetence”. The principle is based on the observation 
    that in such an organization, new employees typically start in the lower 
    ranks, but when they prove to be competent in the task to which they are 
    assigned, they get promoted to a higher rank.<br>
    <br>
    This process of climbing up the hierarchical ladder can go on indefinitely, 
    until the employee reaches a position where he or she is no longer 
    competent. At that moment the process typically stops, since the established 
    rules of bureaucracies make that it is very difficult to “demote” someone to 
    a lower rank, even if that person would be much better fitted and more happy 
    in that lower position. The net result is that most of the higher levels of 
    a bureaucracy will be filled by incompetent people, who got there because 
    they were quite good at doing a different (and usually, but not always, 
    easier) task than the one they are expected to do. <br>
    <br>
    The ‘Red Queen Principle’, the evolutionary mode of ‘Peter Principle’, is 
    less pessimistic in its implications, since evolution lacks the bureaucratic 
    inertia that pushes and maintains people in an unfit position. But what will 
    certainly remain is that systems confronted by evolutionary problems will 
    quickly tackle the easy ones, but may tend to get stuck in the difficult 
    ones. The better (more fit, smarter, more competent, more adaptive) a system 
    is, the more quickly it will solve all the easy problems, and vice versa. In 
    conclusion, the ‘Red Queen Principle’ would make sure that new, more complex 
    problems would arise, so that the species would continue to balance on the 
    border of its domain of competence — or incompetence!</font></td>
