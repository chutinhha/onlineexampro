                <p align="justify"><font face="Verdana" size="1"><b>SOHAR INDUSTRIAL PORT PROFITABILITY ROADMAP<br>
                </b><br>
            At the September Board meeting of Sohar Industrial Port Company, 
            Hans Smith, CEO of Rotterdam Port (which owns 50 per cent of the 
            project with the Government of Oman), underlined the physical 
            progress on the ground, the moves being made to bring the port into 
            profit and the jobs the two partners expect to be created.<br>
                <br>
            Media reports quote Smith has having referred to the new 
            ‘magnificent’ container port — with large gantry and small cranes 
            already installed. “I saw a lot of people training with new 
            equipment,” Smith was quoted as saying. The container port is 
            expected to be opened by end-2006.<br>
                <br>
            Smith is quoted to have mentioned that the biggest challenge is to 
            make sure that the Port of Sohar develops as an international hub. 
            Over and above the captive cargo related to the industries that are 
            there at Sohar, and those in the pipeline, the challenge is to 
            attract “extra cargo”.<br>
                <br>
            “The geographical is right — before the Strait of Hormuz and with a 
            thriving hinterland,” Smith suggested. “It makes sense to bring the 
            cargo to Sohar, for instance from China, by large container ships. 
            Part of the load will be put in smaller ships to continue its 
            journey elsewhere to the Arabian Peninsula, Asia or East Africa. And 
            part of the load will travel by road to, for instance, to Muscat or 
            the Emirates,” the Rotterdam Port CEO added.</font></td>
