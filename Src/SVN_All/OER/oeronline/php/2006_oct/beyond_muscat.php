                <p align="justify"><font face="Verdana" size="1"><b>DEVELOPMENT OF DUQM PORT SET TO KICK OFF 
                </b> <br>
            Construction of a major port and dry dock complex at Duqm on the 
            Wusta coast is set to get under way in earnest, with the government 
            launching the competitive bidding process for a contract covering 
            the first phase development of the ambitious project. In the first 
            phase, a contractor will be appointed to undertake the maritime 
            works package entailing the construction of breakwaters and quay 
            walls as well as carrying out dredging and reclamation works. 
            Landside and infrastructure facilities will be established under a 
            separate contract to be awarded in the second phase, while a ship 
            repair facility — the centerpiece of the Duqm Port complex — will be 
            constructed in the third phase. <br>
                <br>
            Duqm Port will be established at Ras Duqm, some 7 km from Duqm town. 
            The site includes the imposing headland of Ras Duqm, a smaller 
            headland some 2 km northwest of Ras Duqm and the seabed within the 
            bay in between. Duqm is being conceived as a main maritime gateway 
            that will serve an ambitious industrial and commercial hub 
            envisioned in the Duqm area and its hinterland. When completed, the 
            facility will stimulate development in this remote part of the 
            Sultanate, as well as bring employment opportunities to this area.
            <br>
            Eleven firms and their joint venture partners have so far collected 
            tender documents for the first phase maritime works contract. Royal 
            Haskoning, in association with Khatib and Alawi, are consultants for 
            the project, which is being overseen by the Ministry of Transport 
            and Communications. In the fray are well-known contracting firms 
            such as Larsen &amp; Toubro (Oman) with Dredging International; Hani 
            Archirodon; galfar Engineering and Contracting; Hyundai; Van Oord; 
            Consolidated Contracting Company and Sezai Turkes — Feyzi Akkaya 
            (STFA); Boskalis Westminster Middle East; Great Lakes Dredge and 
            Dock Company with Carillion Alawi; Six Construct Company with 
            Hindustan Construction; MT Hojgaard Middle East; and Penta Ocean 
            Construction Company with Gammon India.<br>
                <br>
            The selected contractor will be required to construct two 
            breakwaters of a length of around 5 km. <br>
            He shipyard operator will oversee the construction of the facility 
            in the third phase of the Duqm Port project. The dry dock will take 
            advantage of the port’s proximity to busy regional sea-lanes 
            traversing Oman’s coastal waters.</font></td>
