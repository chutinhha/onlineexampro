            <p align="justify" style="margin-left: 5px; margin-right: 10px"><b><font face="Verdana" size="4" color="#FF0000">
            <br>
            </font></b><font face="Verdana" style="font-size: 14pt"><b>
            Industrial catalyst<br>
            </b></font><font face="Verdana" size="1"><b><br>
            Sohar Refinery Company, in many ways, is the biggest symbol of the 
            region’s industrial growth saga</b><br>
            <br>
            Sohar Refinery Company (SRC), the US$1.25-billion-refinery, is 
            poised to act as a catalyst in developing several downstream 
            petrochemical industries in the Sultanate, especially a 
            polypropylene venture and an aromatics project. Both these ventures 
            depend on the refinery for their feedstock. The state-of-the-art 
            refinery not only adds value to the Sultanate’s rich oil resources 
            but also stands to enhance the contribution of industries in the 
            gross domestic product (GDP).<br>
            <br>
            Considered as the biggest project in the Sultanate after the 
            successful implementation of Oman LNG, as much as 90 per cent of the 
            project cost was funded by way of term loan; the balance was equity 
            capital brought in by two promoters — the Ministry of Finance and 
            Oman Oil Company.<br>
            <br>
            The Japanese Bank for International Cooperation provided US$261.9 
            million as term loan, while 10 regional and international banks 
            undertook to provide a loan of US$907.8 million. The willingness on 
            the part of international lending institutions to offer such huge 
            funds show the confidence of these institutions on the refinery in 
            particular and the country’s robust economy in general.<br>
            <br>
            As the other downstream projects depend a lot on the refinery, its 
            timely implementation is a key factor. Better coordination among 
            various ministries and departments has ensured that work on the 
            refinery proceeds as per schedule.<br>
            <b><br>
            Unique Aspects</b><br>
            SRC has a crude unit with a capacity of 116,400 barrels per day 
            (bpd) and a residue fluid catalytic cracking unit with a capacity of 
            75,260 bpd. Major refined products include gasoline, propylene, LPG, 
            naptha, kerosene and fuel oil. The promoters selected Japan’s JGC 
            Corporation as engineering, procurement and construction (EPC) 
            contractor, while LG is the operation and maintenance (O&amp;M) 
            contractor.<br>
            <br>
            LG brings its world-class expertise in oil refining. It is also 
            working to help the company in motivating the workforce, as also to 
            boost Omanisation and training — both crucial elements. LG has been 
            sending several Omani engineers of Sohar Refinery to its Korean 
            facility for training programmes, which is in line with a government 
            strategy to create a pool of skilled petrochemical professionals.<br>
            <br>
            The refinery is unique in a sense, for it operates as a 
            toll-processing unit in association with the state-owned Oman 
            Refinery Company (ORC). It complements Oman Refinery Company’s Mina 
            al Fahal plant in the production of petroleum products for both 
            domestic and international markets.<br>
            <br>
            Sohar Refinery feedstock will be delivered by Oman Refinery via a 2 
            60-km long 24-inch pipeline from Mina Al Fahal in Muscat. The 
            pipeline was built by a consortium of Saipem/Consolidated 
            Contractors Company (CCC), at a capital expenditure of US$88 
            million.</font></p>
            <p align="center" style="margin-left: 5px; margin-right: 10px">
            <img border="1" src="bemuscat.jpg"></p>
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1">Apart from the pipeline, ample crude 
            storage facilities have been created at both the refineries to 
            ensure uninterrupted crude oil supply. The refined products are 
            being exported after meeting domestic demand.<br>
            However, there could be a change in the earlier plan to export 90 
            per cent of the refined products due to the significant growth in 
            domestic demand for petroleum products. The refinery has already 
            started shipment, and as many as 10 consignments of varying products 
            have been exported to different countries during the start-up and 
            commissioning stage.<br>
            <b><br>
            Downstream Linkages</b><br>
            Last September, a joint venture oil trading firm — Oman Trading 
            International (OTI) — was floated by Oman Oil Company and Vitol to 
            export SRC’s refined products for a five-year period.<br>
            <br>
            On the downstream side, SRC produces about 327,000 metric tonnes of 
            propylene per day, which will be supplied as feedstock to Oman 
            Polypropylene (OPP) coming up near the refinery. Also, the refinery 
            will supply naphtha and gasoline as feedstock to Aromatics Oman, 
            which is also coming up near SRC.<br>
            <br>
            The US$313 million Oman Polypropylene is expected to play a key role 
            in facilitating development of a cluster of plastic industries 
            within Sohar industrial area. The project — jointly promoted by Oman 
            Oil Company, Bahrain-based Gulf Investment Corporation and LG 
            International — is in an advanced stage of completion.<br>
            <br>
            The plant will have an installed capacity of 340,000 tonnes of 
            polypropylene per annum in pellets form, covering various grades for 
            a variety of applications. OPP has an offtake agreement with LG 
            International for exporting its products — and the latter is 
            planning to sell it in the regional market, especially Pakistan. 
            Most of the output of OPP will be exported, with the remaining 
            quantity used locally by ancillary industries.<br>
            <b><br>
            Investment Initiatives</b><br>
            Also, there is a move to develop the local market for polypropylene 
            — a product that is used on a daily basis for food packaging, flower 
            wrappings, furnitures and bottles, to name a few. The introduction 
            of polypropylene industry in Oman is a launch pad for companies 
            interested in investing in downstream polypropylene businesses.<br>
            <br>
            Oman Polypropylene will provide all technical support for those 
            interested in getting into the business or those aiming at expanding 
            their existing businesses. Likewise, the US$1.6 billion aromatics 
            company is setting up a project to produce 814,000 tonnes of 
            paraxylene and 210,000 tonnes of benzene per year.<br>
            <br>
            Paraxylene is used predominantly in producing polyester. Benzene is 
            used in a variety of products ranging from plastics to 
            pharmaceuticals. The project, coming up near Sohar Refinery, is 
            jointly owned by Oman Oil Company, ORC and LG International. The 
            government is planning to set up a ‘Value Park’ to encourage the 
            downstream projects using products of large gas-based industrial 
            ventures like polypropylene and aromatics. These downstream 
            industries are small to medium in nature with capital investment 
            ranging from half-a-million dollars to US$10 million.<br>
            <br>
            Another initiative is from Oman Oil Company, which is floating an 
            investment company to encourage downstream industries in Sohar. The 
            proposed firm will promote small downstream units — using 
            paraxylene, benzene, polypropylene and aluminium. It will also act 
            as a facilitator in arranging technology and funds for the local 
            entrepreneurs.<br>
            <br>
            As these industries are heavily labour intensive, they could 
            accelerate employment generation. Besides, the entire chain of the 
            industry makes an important contribution in the form of capital 
            formation and technological skill up-gradation.</font></p>
