            growth, says a media report published recently.<br>
                <br>
            The company is soon to launch its own brand of a delightful range of 
            halal meat products that are made from top quality beef, chicken and 
            lamb — and will be made from the best farms of Oman as well as 
            around the world. The production facility in Sohar comprises the 
            very latest equipment, assuring optimum hygiene standards.<br>
                <br>
            The products will also use state-of-the-art packaging, attractive 
            labelling, great taste, longer shelf life, bar coding on individual 
            products, nutritional facts explained on labels and special cooking 
            instructions.</font></td>
