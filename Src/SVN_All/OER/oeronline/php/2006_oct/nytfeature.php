      <p align="justify" style="margin: 10px"><b>
      <font face="Verdana" style="font-size: 14pt">
      The Secrets of Success</font><font face="Verdana" size="1"><br>
      <br>
      How do you make this magic your own? Here are the tips:</font></b><font face="Verdana" size="1"><br>
      <br>
      <br>
      1. Have passion for the business<br>
      Entrepreneurs simply care more about the businesses they create and grow 
      than most ‘hired hand’ CEOs. “It would be truly devastating to me if this 
      company ever failed,” says Jim Sinegal, co-founder of Costco. And as 
      McDonald’s founder, Ray Kroc, loved to say, “You’ve gotta see the beauty 
      in a hamburger.”<br>
      <br>
      So the challenge for non-founder CEOs and their professional managers is 
      to find the beauty in their own work. A great place to start is to do 
      something you love. <br>
      <br>
      2. Be a product/market guru<br>
      If you invent it, make it and sell and service it yourself, you are the 
      guru that the market listens to and the employees idolise. You are 
      Soichiro Honda, auto mechanic and race-car driver, making the most 
      wonderful machines in the world. You are Walt Disney, believing, “The 
      inclination of my life has been to do things and make things which will 
      give pleasure to people in new and amazing ways.” The good news is that 
      all corporate leaders can become product/market experts and champions in 
      their own right.<br>
      <br>
      3. Act fast<br>
      Akio Morita, the founder of Sony, called moving quickly the entrepreneur’s 
      secret weapon. Consider Chiron co-founder Edward Penhoet’s race to 
      discover the vaccine for Hepatitis B. Years after Chiron had developed the 
      vaccine, he told me: “Biotech is a horse race. Everyone in the industry 
      knows what the opportunities are, but the winners are the ones who get to 
      the finish line first.”<br>
      <br>
      Perhaps the best advice comes from Larry Hillblom, co-founder of DHL, who 
      told me how he started up 120 country offices over the course of a decade 
      with no financing, headhunters or even a business plan. “We did it because 
      we had to,” he explained. “You have to learn to do what all the 
      consultants and bankers tell you is impossible.”<br>
      <br>
      4. Cultivate commitment: A two-way street<br>
      Most entrepreneurs simply won’t tolerate sloppy work. But they also 
      practice what IBM’s Thomas Watson called “respecting the dignity of every 
      employee.” No modern day entrepreneur does that better than John Mackey, 
      with his 33,000 ‘team members’ at Whole Foods. Here are some of Mackey’s 
      paradigm-busting practices:<br>
      Every three years, all employees vote to choose the health-and-benefits 
      package they want the company to offer.<br>
      <br>
      The company awards 90 per cent of all stock options to non-executive 
      employees.<br>
      It sets a salary cap on executive compensation at 14 times the average pay 
      of full-time personnel.<br>
      <br>
      It opens the financial books to all employees and publishes everyone’s 
      compensation, including executives’.</font></td>
