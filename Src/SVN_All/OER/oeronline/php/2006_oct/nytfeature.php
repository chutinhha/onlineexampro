<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>All in the family<br>
</b>
</font><font face="Verdana" size="1">
<b><br>
Entrepreneur versus professional-manager models. The former is clearly coming up 
trumps.<br>
</b><br>
By Larry Farrell<b><br>
</b><br>
</font><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="nyt.jpg" width="100" height="145" align="left" hspace="5" vspace="2">T</b></font><font face="Verdana" size="1">he 
evidence is pretty overwhelming. The entrepreneur-led model of running a 
business is outperforming the professional-manager model. What are entrepreneurs 
doing that’s putting their businesses on everyone’s rosters of hot companies?<br>
<br>
First, the evidence:<br>
Since the first Fortune 500 listing, back in 1955, about 70 per cent of the 
original companies have fallen off the list — always replaced by newer and more 
entrepreneurial upstarts. And that trend is accelerating. Of the hundred biggest 
companies on this year’s list, a startling 30 per cent were either unknown 
startups or not even in existence three decades ago.<br>
<br>
To top it off, within the Fortune 500, the companies that are still being led by 
their founders are far outperforming the others: Over the past decade, the 
entrepreneur-led firms had an average annual profit growth of 19.6 per cent 
versus 11.7 per cent for the entire 500.<br>
<br>
Then there’s the fact that one-third of the BusinessWeek 50 — an annual ranking 
of America’s top growth companies — are led by their founders. Apple Computer 
leads the list, followed by entrepreneurial stalwarts such as Qualcomm, Amgen, 
Cisco Systems, Starbucks and FedEx. Indeed, if you cut the oil businesses from 
the roster, the entrepreneurial companies make up half the remaining list.<br>
<br>
BusinessWeek’s November 2003 special report Family, Inc. supports the trend: 
“Surprise! One third of the S&amp;P 500 companies have founding families involved in 
management,” either as chairman, CEO or significant shareholders, noted the 
report. “And, in what may be Corporate America’s biggest and best-kept secret,” 
it continued, “they’re beating the pants off their non-family-run rivals.”</font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" style="font-size: 11pt">
<b>
<img border="0" src="nyt1.jpg" width="300" height="189" hspace="5" vspace="2"></b></font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">These 177 family-run companies are large, established enterprises like 
Walgreens, Wrigley, Oracle, FedEx, Anheuser-Busch, Wal-Mart and Microsoft. The 
10-year financial comparisons are dramatic — annual profit growth of 21.1 per 
cent versus 12.6 per cent for non-family S&amp;P companies.<br>
<br>
Finally, I recently reviewed Fortune’s 100 Best Companies to Work For, seeking a 
common thread. I found one just reading down the list of names: Genentech, W.L. 
Gore, J.M. Smucker, SC Johnson, Whole Foods Market, Cisco Systems, Starbucks, 
Microsoft. The list is jam-packed with family and entrepreneur-led companies. It 
shows, again, that entrepreneurial companies are not only growth leaders but 
also beacons of good employee practices.<br>
— Distributed by The New York Times Syndicate<br>
<i><br>
Larry Farrell is the founder of The Farrell Co., a worldwide organization that 
researches and teaches entrepreneurial practices. His most recent books are 
Getting Entrepreneurial! (John Wiley &amp; Sons, 2003) and The Entrepreneurial Age 
(Allworth Press, 2001).</i></font></p>
