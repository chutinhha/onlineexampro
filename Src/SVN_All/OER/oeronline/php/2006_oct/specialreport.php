<p align="justify" dir="ltr"><font face="Verdana" size="3" color="#FF0000">
<br>
</font><font face="Verdana" style="font-size: 14pt"><b>Better Work Through 
Occupational Health</b></font><font face="Verdana" size="1"><br>
<br>
<b>It’s important to have a healthy, safe, productive, motivated and 
cost-efficient workforce. That’s the message brought home at a recent discussion 
forum on Occupational Health organised by Petroleum Development Oman (PDO) — an 
exclusive OER curtain-raiser to an unprecedented conference coming up this 
December in Oman. Reported by Sarada Vishnubatla</b></font></p>
<p align="center" dir="ltr">
<img border="1" src="special.jpg" width="400" height="210"></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he 
International Labour Organisation (ILO) estimates that approximately four per 
cent of the world’s gross national product is spent on treating illnesses and 
injuries that people somehow acquired from their work. It is therefore a 
straightforward deduction that proper occupational health can improve 
productivity, improve the health of workers and reduce medical expenditures.<br>
<br>
Responding to the occupational health situation both globally and nationally, 
Petroleum Development Oman (PDO) has taken the initiative in spreading awareness 
among the policy-makers, workers, managers and government officials. The 
Sultanate’s first Occupational Health Conference is being organised in Muscat on 
11–13 December 2006 by PDO. The event will bring together international, 
regional and local experts to promote occupational health and highlight its 
relationship to employees’ productivity and well-being. A range of issues for 
discussion is planned including Fitness-To-Work standards and the growing burden 
of sickness absence and ill-health retirement on Oman’s economic development. 
The conference aims to conduct plenary sessions, panel discussions and workshops 
to provide meaningful brainstorming sessions on possible solutions.<br>
<br>
As a prelude to the occupational health conference, PDO organized a discussion 
on the topic in the second fortnight in September. The invited panellists 
discussed occupational health measures currently being practiced in Oman and the 
various lacunae in its implementation. <br>
<br>
OER exclusively publishes here excerpts of the intense wide-ranging discussion:<br>
Moderator: To get the ball rolling, may I ask you, Dr Nasser, to define the term 
“occupational health”?<br>
<b><br>
Nasser:</b> The World Health Organisation (WHO) defines health as well-being — 
physically, mentally and socially. In terms of occupational health, one might 
also add occupationally and educationally to that definition. In any case, it’s 
essential to regard it as a holistic phenomenon. Why so? Because we don’t go to 
work as an arm or a leg; we go to work as a whole human being.<br>
<br>
And it’s not just a problem pertaining to work. Take, for example, stress. How 
many times have we taken work problems home and talked about them to our family 
members? Conversely, we also take domestic problems to work and talk to our 
colleagues and supervisors about them there. This is an example of a cumulative 
dose of stress that adds up, from wherever you go and whatever you do. <br>
<br>
At the end of the day, it’s not only your family that suffers. The consequences 
affect the employers too, because they affect the employees’ productivity.<br>
<b><br>
Moderator: </b>Dr Salim, from your Ministry’s perspective, would you agree with 
Dr Nasser’s definition and would you say that it is being applied in practice — 
particularly outside the oil and gas industry?<br>
<br>
Salim: Yes, I would agree with Dr Nasser’s definition. But I would broaden it a 
bit. The ILO defines work-related injury as an injury caused even during the 
time of travelling from home to work and vice versa.<br>
<br>
The labour law of the Ministry of Manpower certainly includes occupational 
health. So the law is there, but the practice of occupational health and safety 
in industries other than oil and gas was virtually non-existent till 2004. In 
2004 the National Committee for Occupational Health and Safety was set up by the 
Ministry of Manpower. It included all the ministries, public and even private 
companies. But there was no member from the Public Authority for Social 
Insurance (PASI). <br>
<br>
Saleh: Thank you. (Everyone laughs.) As a social-insurance organisation, PASI 
hopes for the best, but in practice we are concerned not about occupational 
health as much as we are about occupational disability. We define occupational 
disability as a disability that happens at the workplace or because of work. 
That means either an accident or occupational disease caused directly or 
indirectly by the nature of the job. <br>
<br>
Aqeel: So the oil and gas sector has implemented occupational health initiatives 
but other industries have not. Why?<br>
<b><br>
Salim: </b>When we set up the National Committee for Occupational Health and 
Safety, we visited and evaluated most of the industries other than oil and gas. 
We had to have a good database, and we had to evaluate <br>
<br>
the occupational health hazards in other industries. Once that was completed, we 
drafted legislation to cover other industries. So we are now waiting for the 
ministerial decree to publish this law and so that it can be duly implement.<br>
<br>
Moderator: Jamal, does the Government have any mechanisms at its disposal to 
enforce compliance? <br>
<b><br>
Jamal: </b>We have a ministerial decision issued in 1982 on occupational health 
and safety regulations applicable to the private sector. We try to update work 
standards there to match occupational health and safety with the best 
international practices. We help employees and employers comply with the 
standards, by trying to make them realise their responsibilities. The Department 
of National Employment conducts interviews for both employees and the employers. 
There are employees who will go through the training programs and interviews. 
And they will have to pass different examinations to see if they are fit to 
work. <br>
<b><br>
Moderator: </b>But how would you compel employers to think about the 
occupational health of their workforce? <br>
<b><br>
Jamal: </b>The legislation of 1982 says that the Ministry of Manpower should 
enforce a rule that the employer should provide a safe and healthy work 
environment.<br>
<b><br>
Moderator:</b> Presumably, there are things like penalties….<br>
<b><br>
Jamal:</b> Yes. Sometimes penalties, sometimes their work transactions are 
stopped by the Ministry until their standards of work environment safety matches 
the standards laid out by the Ministry.<br>
<b><br>
Saleh: </b>A couple of weeks back, I watched a news programme on TV from one of 
the Arab countries, not in Oman. The anchor interviewed an employee about the 
disease he was suffering from. He said, “My illness was caused by hazardous 
materials used in the factory. I am not the first one, about 12 to 13 of my 
peers have expired because of the same reasons.” The important thing here is 
that if such cases go to the court, it will cost the employers an unbelievable 
amount in compensation for disabilities. So raising awareness is beneficial for 
both employees and the employers themselves.<br>
<b><br>
Moderator: </b>Aqeel, I was wondering if the Ministry of National Economy has 
done any calculation on the cost of occupational illness…<br>
<b><br>
Aqeel: </b>Well, we don’t have any economic scenario to cover this issue, but we 
have to encourage people to become aware. We must increase information 
dissemination on how to prevent these kinds of injuries.<br>
<b><br>
Moderator:</b> PDO is organising an Occupational Health Conference to that end 
in December this year. Abdul-Amir, please explain why PDO has taken the lead in 
this regard.<br>
<b><br>
Abdul-Amir:</b> We have two main objectives — one internal and the other 
external. PDO has established some rules and regulations covering occupational 
health in order to achieve a balance between its productivity and the welfare of 
its employees. So the internal objective is that we increase the awareness of 
occupational health amongst our employees and amongst our business partners, 
namely the contractors that work with us. The second, external objective is that 
we share our knowledge and experience with other industries. The target audience 
of this conference is not only people in the oil and gas industry but also 
people in other industries. <br>
<b><br>
Moderator:</b> What has PDO done so far to educate its workforce?<br>
<b><br>
Nasser:</b> We strongly believe that occupational health is not a luxury for any 
organisation. We address both the top management and the bottom-rung employees. 
With top management, we focus on the totality of the benefits of health. 
Therefore, whenever we get an argument — like “But it’s expensive; occupational 
health is costly.” — we ask a question: “Do you know how much it would cost 
without occupational health?” Occupational illnesses and sickness-absences can 
have enormous impact in terms of loss of money and expertise. When the right 
people fall sick, sometimes the wrong people take over and cause even more 
damage.<br>
<b><br>
Moderator:</b> What are the educational and training opportunities through 
universities like Sultan Qaboos University for professionals in occupational 
health?<br>
<b><br>
Mohammed:</b> Unfortunately, we don’t have any sort of courses and programmes 
specifically for occupational health. At the undergraduate level, our medical 
students learn about diseases related to occupations, such as lung cancers. At 
the post-graduate level, we have a residency programme in family medicine and 
community health. And we usually send our residents to Dr. Nasser for training. 
But we do have plans for the future.<br>
<b><br>
Moderator:</b> Are we talking about doctors and nurses? Or are we talking about 
other types of people?<br>
<b><br>
Salim: </b>If I can answer… We have a training plan for 2006-2007 — a joint 
programme with WHO in which occupational health is one of the priority 
programmes. We will start the first National Training Workshop for occupational 
safety inspectors as master trainers in Oman. They will include officials form 
the Ministry of Manpower, inspectors from Ministry of Health and certain other 
industries. This workshop will be held immediately after the December 
conference.<br>
<b><br>
Moderator: </b>I suppose the foregoing in part answers Aqeel’s earlier point as 
to why more industries are not included in the Government’s occupational health 
scheme. To get a proper system in place, requires quite a bit of preparatory 
work and training.<br>
<b><br>
Salim: </b>Yes. And part of the preparation is the compilation of data. We have 
a surveillance system for occupational injuries. Everybody, by law, has to 
report to us immediately about an injury. We started this in January 2006. We 
know how much each injury costs the Ministry. On average, the treatment cost for 
a patient’s injury costs between 2500 and 3000 rials. This does not include the 
loss of jobs or other things. Not all institutions report such injuries, as they 
are scared of the legal consequences. This year, 400 injuries have been reported 
— from mild to severe — with three deaths. But this is the tip of an iceberg. We 
did a national profile as asked by the WHO. We tried to get information on 
injuries from different sectors. But it’s very difficult to get this information 
and difficult to get accurate data. From PASI we can get data only about Omanis. 
So, we decided to set up this system in the Ministry of Health.<br>
<b><br>
Moderator: </b>Saleh, what sort of data does your organisation provide?<br>
<b><br>
Saleh: </b>We started a branch of insurance in January 1997. And to be a member 
in this scheme, an employer has to contribute one per cent of the basic salary 
of each of his employees. According to our studies, that contribution rate is 
enough. As of now, this is only for Omanis. But we are in the process of 
studying how to include expatriates as well. The one per cent will allow you to 
get certain benefits. For example, the total benefit for total disability or 
death is 75 per cent of your wages, even if you have been only one day in the 
member organisation.<br>
<br>
This year 300 claims have been submitted. In previous years, not even half of 
this number were reported because of a lack of awareness. In fact, from 1997 
till now, no more than 1000 cases have been reported.<br>
<b><br>
Salim: </b>But this is only for Omanis. The country’s labour force includes both 
Omanis and non-Omanis. So the cost will be more. Most of the cases that we get 
from health centres are expats. Of 400 cases reported to the Ministry of Health 
from January to September, 303 concerned expats. This is indeed the tip of the 
iceberg. Not all injuries are reported.<br>
Moderator: The goal of the Occupational Health Conference in December is to 
increase the awareness among both expats and Omanis about the entitlements that 
are available. Will it therefore increase the number of cases reported, and have 
PASI and the Ministry of Health taken that into account?<br>
<b><br>
Saleh: </b>If people know their rights, the level of claims will certainly rise. 
We are doing a publicity campaign for the community to let them know that they 
need to be insured in PASI — and also about social insurance as a compensation 
system. <br>
<b><br>
Moderator: </b>To wrap up this discussion, may I ask all participants to 
describe your dreams for your respective organisations?<br>
<b><br>
Salim: </b>Our dream is to have an independent occupational and safety 
organisation that takes care of all the aspects of occupational health and 
safety.<br>
<b><br>
Mohammed:</b> As we are an academic institution, we look to do research with 
co-operation from industries. Occupational health problems account for the most 
of the disabilities, mortality and morbidity worldwide. Research can play an 
integral role in furthering our understanding of these problems. It can assist 
in anticipating, evaluating, treating, preventing and controlling these 
problems. We can exchange our occupational health data with other developing 
countries, such as China, India and Brazil.<br>
<b><br>
Abdul-Amir:</b> We need more events like this. PDO would love to continue 
working with all partners, but we don’t want to monopolise the process. We are 
only doing this through partnerships. <br>
<br>
Jamal: My dream would be to have both employees and employers be equally aware 
of occupational health.<br>
<b><br>
Aqeel: </b>Well, I have two dreams. For the short term, to prepare a detailed 
feasibility study addressing all the issues mentioned here. For the long term, 
to form committees at a higher level to permit some of the recommendations to be 
implemented. We can also work together to form a data warehouse in our ministry 
for internal and external users.<br>
<b><br>
Nasser: </b>I would like to see occupational health recognised more formally and 
practically. If that happens, then my next dream would be fulfilled: that Oman 
has a healthy, safe, productive, motivated and cost-efficient workforce.<br>
<b><br>
Saleh: </b>Well, if everybody gets their dream fulfilled, then we would not need 
PASI any more. [Laughter.]<br>
<br>
Seriously, though, we need to know the levels of the “iceberg” of occupational 
health. I think there are some hidden areas and we must know them through the 
upcoming conference. The second thing that we must aspire for is a national 
health insurance, which is a kind of social insurance but falling under the 
Ministry of Health. Moderator: Thank you all very much for your insightful 
comments on this interesting issue.</font></p>
