<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" style="font-size: 14pt">
<b>New look, better performance<br>
</b>
</font><font face="Verdana" size="1">
<b><br>
With its revamped looks and performance, the 2007 Toyota Camry has clearly 
raised the bar when it comes to mid-sized sedans. Anne Kurian test-drives the 
much talked-about car.</b></font></p>
<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="car1.jpg" width="400" height="263"></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" style="font-size: 11pt">
<b>I</b></font><font face="Verdana" size="1">f there’s one thing in today’s 
world that most people seek and try to attain, it’s comfort. Be it the way we 
live, our work or even when it comes to the cars we drive. The new 2007 Toyota 
Camry is an embodiment of comfort among other things. The sedan that has got the 
Americans going crazy for many years has also got a loyal following in Oman. And 
the new Camry is definitely going to be a great advertisement for Toyota again.
<br>
<br>
The 2007 Camry is the sixth generation of the Camry cars. It sports a redefined 
and stylish look with improved legroom and better driving capabilities. Now, 
this translates into what we started with — comfortable drive, a feature that 
all Toyota cars are famous for. The Camry has been one of the fastest selling 
cars in the Toyota family and it has only improved with every generation 
launched. The 2007 model in fact surpasses expectations in the key areas — 
styling, performance and road handling. <br>
<br>
The Camry comes in three variants —GL (M/T and A/T), GLX (A/T) and GLX SPL 
(A/T), the latter being the all-feature included model. We took the GLX for a 
spin and came back impressed. <br>
<b><br>
The Inside-Outside Look</b><br>
The new Camry has become bigger yet looks leaner with a definitive athletic 
body. The length of the car has been increased by 10 mm while the wheelbase has 
gone up by 50mm. That’s slightly more room for people. Yet, what catches your 
eye first is its aerodynamic design. At times the Camry has been joshed for its 
boxy look. This time round Toyota has made sure that it revamps the model to go 
with the features that get everyone’s pulse raising — youthfulness and sporty. 
The fascia has a sharper, and racy look to it, a much sought after feature in 
cars today. <br>
<br>
The new, improved sedan’s popularity is no doubt going to rest a lot on the 
redesigned shape. Even the rear of the car has been revamped to go with the 
overall young feel of the Camry. The doors have a body colour with silver accent 
running across which may be only a cosmetic addition but it sure does give the 
Camry some pizzazz. Minor though it may seem, but you can’t help notice the way 
the rear lamps wrap around in the back, giving it a dash of elegance. <br>
<br>
We then proceeded to get behind the wheel. And that’s when we were informed that 
there will be no more bumping against the steering wheel when entering a car, a 
problem many drivers face. For, the new Camry’s steering wheel has been 
ergonomically designed to suit driver comfort. <br>
<br>
In fact, even the interiors have been well crafted, with front seats being 
redesigned to provide better traveling experience. The roominess of the car also 
extends to the back where besides having more leg space, it also has a lower 
central floor bump. Camry scores a point against its competitors with the 8-way 
adjustable front seat system. This feature is usually available in vehicles 
belonging to a higher category. <br>
<br>
The controls are aesthetically placed on the dashboard. And to make the ride all 
the more comfortable, there are steering mounted control switches.<br>
<br>
There’s more to say about the Camry with regard to its features that include the 
bluetooth telephone kit, CD Changer with 6 speaker system that is MP3/WMA 
compatible, cruise control, multi-information display etc. Instead of stating 
all the features here, it would be better to say that in the new Camry plain is 
out and jazz is in.<br>
<b><br>
On The Roads</b><br>
Better engine, more power. That’s Camry’s performance in a nutshell. The new 
Camry has been fitted with a 2.4-litre, 4-cylinder EFI engine with WTi, which 
means there’s quite a bit to expect. The car rides smooth on the road. The sedan 
has good road handling capabilities, especially at turns and curves, though 
that’s not something you would want to overdo. Over slightly uneven grounds, 
surprisingly, the Camry does not let you feel the irregularity.<br>
<br>
The steering provides for a very firm ride and proves to be tougher at high 
speeds and at the roundabouts. In the new Camry, there won’t be a moment when 
you feel out of control. We had taken the 5-Speed A/T Camry that also came with 
the multi-mode transmission. It’s also the first time that the Camry is being 
launched with the 5-Speed A/T. Switching between the two, the Camry dished out 
an impressive performance in both transmissions. Though the Camry has been 
viewed as a mid-size sedan that families prefer for its reliable performance, 
make no mistake! The 2007 Camry can handle aggressive driving too. <br>
<br>
One aspect about the car that delighted us was the quietness inside when in 
motion. Now, most car enthusiasts would know this is a feature that many 
high-end vehicles come with. So it’s an added advantage in the Camry, especially 
because few of its competitors have this functionality.
</font></p>
<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>On The Safer Side</b><br>
The main safety features that one would want in one’s car but might not find in 
sedans in this category, are packed into all the three variants of the Camry — 
Anti Lock Brake System (ABS), Brake Assist (BA), Electronic Brakeforce 
Distribution (EBD), anti-theft engine immobiliser system and a stiffer body.<br>
<br>
Overall, the Camry experience shows Toyota has raised its own bar. Comfort is 
undoubtedy on of its strengths. And to top it, the car’s youthful look moves 
onto its driving aspects too. Along with the families who go in for the Camry, 
Toyota might just end up getting the younger generation in with this new 
revamped model.</font></p>
