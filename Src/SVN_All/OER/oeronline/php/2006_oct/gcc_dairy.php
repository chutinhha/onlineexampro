        <font face="Verdana" size="1"><b>Saudi Arabia <br>
        Solid performance</b><br>
        Taking advantage of overall growth of the economy, the banking sector is 
        expanding its wings and growing fast in Saudi Arabia as it is among the 
        most profitable sector in the GCC — with the return on average assets 
        and return on average equity at 4 per cent and 33.1 per cent 
        respectively, as reported in 2005. With the demand for funds being 
        strong, thanks to the strong economic growth and the announcement of 
        massive infrastructure projects in different sectors, the banking 
        sector’s growth is expected to remain strong.<br>
        <br>
        Saudi Arabian Monetary Agency (SAMA), Saudi Arabia’s financial 
        regulator, has been active in liberalising the sector and has issued 
        licenses to a number of GCC and foreign banks to operate in the Saudi 
        market. During 2002-05, the number of bank branches in the Kingdom 
        increased from 1,203 in 2002 to 1,251 in 2005. The number of ATMs grew 
        much faster — from 3,108 in 2002 to 4,576 in 2005. Alternative delivery 
        channels have gained importance to provide better services to customers 
        and, in turn, help banks reduce transaction cost — thereby enhancing 
        operational efficiency.<br>
        <br>
        The Global Investment House report says that the consolidated assets of 
        the banks under review grew substantially during 2002-05. Now, liberal 
        licensing by the authorities will help the banks to increase their 
        number of branches — and thus mobilise more deposits. The report 
        anticipates the earnings from core banking activities to drive Saudi 
        banking growth where consumer loans was the key driver of credit growth 
        during the last three years.<br>
        <br>
        Most banks in the Kingdom have launched Islamic banking products, either 
        through a separate Islamic window or a subsidiary. Islamic banking is 
        turning into a fast-developing, highly profitable banking product. Also, 
        the division between Islamic banks and conventional banks is reducing 
        fast — with many banks having a large chunk of their deposits as 
        non-interest bearing. In fact, the deposit growth of Islamic banks have 
        been higher than of the conventional banking sector.<br>
        <b><br>
        <img border="0" src="saudiaa.jpg" width="200" height="141" align="left" hspace="5" vspace="2">Truly booming</b><br>
        Strong oil exports are expected to result in record current account 
        surplus of US$114 billion, the eighth surplus in a row. Even with 
        government spending likely to increase by 20 per cent over the 2005 
        levels which, in itself, represents strong fiscal stimulus to the 
        economy,the surplus is expected to hit a record US$67 billion in 2006.<br>
        <br>
        According a study conducted by Samba Financial Group, the Kingdom will 
        earn US$202.9 billion in oil exports this year, an all-time record, 
        representing a 25 per cent growth in oil earnings compared to last 
        year’s at US$162 billion. Government debt, all domestically held and 
        riyal-denominated, will decline to around US$101 billion, or 27 per cent 
        of the GDP. Foreign assets at the central bank will grow to around 
        US$224 billion, enough to provide budgetary support for years to come 
        and defend the currency’s peg to the dollar. Overall, the government 
        finances and the Kingdom’s economy are strong, and continue to grow 
        stronger.<br>
        <br>
        Strong conditions such as high oil revenues, simulative fiscal policy, 
        robust non-oil growth, low inflation, and surging investments in major 
        projects are likely to continue well beyond 2006, the report says.<br>
        <br>
        The strong oil revenues will result in strong fiscal and trade surpluses 
        this year, says the report. In merchandise trade, the Kingdom typically 
        imports only about one third of the amount it exports. In 2005, 
        according to preliminary data from Samba, the Kingdom earned US$178 
        billion from exports. The report estimates that merchandise imports were 
        approximately US$54 billion. For 2006, it forecasts merchandise export 
        earnings of US$222 billion and imports of US$68 billion, with a 
        merchandise trade balance of US$154 billion. <br>
        <br>
        Saudi Arabia is truly experiencing a boom, having grown 13 per cent in 
        2003, 18 per cent in 2004, 23 per cent in 2005, and a likely 20 per cent 
        in 2006 -- in each case with about 1 per cent inflation. For 2006, the 
        Samba report forecasts a nominal GDP growth of 19.9 per cent and real 
        GDP growth of 5.84 per cent, both modest upward revisions from its 
        previous forecasts. The nominal GDP growth figure represents another 
        year of strong increases in oil revenues (up by a forecast 25 per cent), 
        along with strong growth in the private sector and government, the 
        report says. <br>
        <br>
        “This year is shaping up to be another record year for fiscal 
        performance, with the largest budget surplus in history likely again in 
        2006 on record revenues and spending and declining debt,” says the Samba 
        report. “There is little doubt that the government will outperform its 
        budget. Our forecast is that the government will end the year with a 
        surplus of US$66 billion,” the report adds.<br>
        <br>
        The pattern of government spending mirrors the nature of the boom so far 
        — driven by growth in government and Aramco spending, but not fully 
        reflective of the magnitude of the rise in oil revenues. <br>
        <br>
        Strong economic growth has helped the government reduce its debt from an 
        alarming 93 per cent of the GDP to 45 per cent of the GDP in 2005. The 
        study predicts that the government will use some of its surplus to 
        reduce the debt further in 2006 -- to 27 per cent of the 2006 GDP.<br>
        <br>
        Another major development in the first half of 2006 was the sharp 
        movement in stock markets. According to the report, although the stock 
        market rattled nerves in the first half of the current year, it should 
        not distract from the broader story of powerful economic performance. 
        The Saudi stock market remains at an early stage in its evolution and 
        faces a challenge in keeping up with the booming economy, rapidly 
        growing corporate finance activity, privatisation, entry into the market 
        of foreign and new domestic financial institutions, financial sector 
        restructuring, and the emergence of an almost universal ‘investor class’ 
        in Saudi Arabia over the past few years, the report says.<br>
        <br>
        A major attraction of the first half of 2006 was mega projects besides 
        the oil and stock market. About 37 major projects that are underway or 
        have a high likelihood of implementation over the next several years, 
        with a total value of over US$426 billion, represent the Saudi 
        government’s commitment to the country’s progress. Defence and security 
        projects as well as security purchases will account for 17 per cent of 
        the total projects.<br>
