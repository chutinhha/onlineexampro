        be given to government employees, 10 per cent will be offered to those 
        companies which are already listed on DFM. The remaining 55 per cent 
        will be offered to the public — open to all nationalities. Though the 
        exact pricing of its shares is not known, Essa Kazim, Director General 
        of DFM, indicated that the DFM shares would be valued at a price to 
        earnings (P/E) ratio of about eight, making them cheaper than those of 
        other listed exchanges.<br>
        <br>
        <img border="0" src="uaea.jpg" width="250" height="249" align="left" hspace="5" vspace="2">Dubai 
