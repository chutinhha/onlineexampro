        </font><b><font face="Verdana" style="font-size: 14pt">Investments yet 
        to make impact</font><font size="1" face="Verdana"><br>
        Despite strong growth, FDI remains crucial to the future of GCC 
        economies</font></b><font size="1" face="Verdana"><br>
        <br>
        </font><font face="Verdana" style="font-size: 11pt"><b>D</b></font><font size="1" face="Verdana">iversifying 
        from the oil sector, the need to develop infrastructure facilities and 
        increasing fiscal pressures have changed the attitude of the Gulf 
        economies towards luring foreign direct investments (FDI).<br>
        <br>
        More investor-friendly attitudes and policies are on the agenda 
        throughout the region. This includes liberalisation measures, divestment 
        of state enterprises, the reduction of trade barriers, and deregulation. 
        Despite ample security risks, FDI inflows doubled from US$14.2 bn in 
        2004 to US$26.8 bn in 2005 in the Middle East region, illustrating how 
        keen the countries are to invest in this part of the world.<br>
        <br>
        According to a report World Investment Prospects: Boom or Backlash? 
        published by the Economist Intelligence Unit and the Columbia Program on 
        International Investment (CPII), “high energy prices should maintain the 
        high levels of liquidity in the Gulf, and boost FDI from the Gulf states 
        into other parts of the region. Regional instability may not have much 
        impact on Gulf investors; it may result in a reallocation of regional 
        FDI, rather than a fall in the overall total coming from Gulf 
        investors.”<br>
        <br>
        In the Middle East, the UAE accounted for a large share (more than 50 
        per cent) of the regional FDI total in 2005, as well as the bulk of the 
        increase in regional investment inflows in 2005 compared to 2004. But 
        the report indicates that there could be a marginal drop in FDI inflows 
        to the UAE — from US$14.5billion to US$13 billion in 2006. It also 
        expects the FDI inflow to slow down further, to US$7.5 billion by 2010. 
        The other economies in the Gulf region are also not making any great 
        impact, says the report, with FDI inflows into the GCC countries likely 
        to increase just marginally.<br>
        Potential FDI inflows into the UAE, the region’s main FDI recipient, are 
        likely to remain strong in the short term, as high oil prices fuel a 
        regional economic boom. However, elements of the business environment 
        may hinder investment, the report adds.</font></p>
        <p align="center">
        <img border="0" src="table1.jpg" width="338" height="173"></p>
        <p align="justify"><font size="1" face="Verdana">Even though attracting 
        FDI has begun to assume importance in other Gulf States, many stumbling 
        blocks remain, the report points out. Some problems, such as the 
        relatively small size of individual markets and skill shortage in many 
        GCC states, will be difficult to resolve in the medium term. For 
        instance, a rapid expansion is placing increasing pressure on the 
        infrastructure, and the UAE is imposing employment quotas that force 
        firms to hire Emirate nationals.<br>
        Though the Gulf governments are moving towards more liberalisation, 
        there’s little impact on the scale of FDI in the GCC states. One 
        possible explanation for this could be the existence of alternative 
        destinations with more liberalised economies. The trend is not likely to 
        change much in the coming years.<br>
        <br>
        In their international ranking of 175 economies around the world, the 
        Doing Business 2007: How to Reform report, published recently by World 
        Bank and International Finance Corporation (IFC), has found that the 
        Gulf states are not so business-friendly.<br>
        <br>
        The WB-IFC report ranks 175 economies on the basis of the “ease of doing 
        business” — having added 20 more economies in this report. The report 
        places Saudi Arabia at the 38th position, down three ranks from the 
        previous year. However, Saudi has been listed as the best Arab country 
        to do business. Other GCC states listed in the report include the UAE at 
        77 (down 9 ranks from the previous year), Oman 55 (down 3) and Kuwait 46 
        (down 6).</font></p>
        <p align="center">
        <img border="0" src="table2.jpg" width="338" height="173"></p>
        <p align="justify"><font size="1" face="Verdana">On the positive note, 
        the GCC states are making changes in their policies. For instance, Saudi 
        Arabia has simplified the process for registering a new company. The 
        changes have reduced business start-up time by almost a month (from 64 
        to 39 days). Similarly, Kuwait has made property registration process 
        easier by hiking the staff at the registry, and reduced the time to 
        transfer real estate titles from 75 to 55 days. </font></p>
        <p align="center">
        <img border="0" src="table3.jpg" width="331" height="146"></p>
        <p align="justify"><font size="1" face="Verdana">Michael Klein, World 
        Bank-IFC Vice President for financial and private sector development and 
        IFC chief economist, says: “More progress is sorely needed. Middle 
        Eastern and North African countries would greatly benefit from new 
        enterprises and jobs, which can come with more business-friendly 
        regulations.”<br>
        <br>
        To attract more investments, experts say that the GCC states need to 
        make policies to create business-friendly climate — such as liberalising 
        the labour market, improving infrastructure, facilitating licensing and 
        documentation, eliminating hidden non-tariff barriers, and more 
        importantly, ensuring fair and equitable treatment of foreign 
        in-vestors, with limited restrictions.<br>
        — By Sowmya Sri Rallabhandi</font></p>
