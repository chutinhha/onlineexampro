        <b><br>
        <img border="0" src="kuwaita.jpg" width="200" height="176" align="left" hspace="5" vspace="2">Kuwait, US to frame FTA schedule</b><br>
        Kuwait and the United States have agreed to set up a timetable for 
        signing a Free Trade Agreement (FTA). The second meeting for the Trade 
        and Investment Framework Agreement (TIFA) held recently made progress on 
        a number of trade and economic issues, informed Kuwaiti Minister of 
        Commerce and Industry Falah Al-Hajri. The meeting focused on six 
        different issues — ranging from the investment and trade sector, the 
        government system on tax reform, bilateral tax agreement, ways of 
        dealing with economic reforms and related specific legislations, to 
        bilateral tax and investment protection — which will be enforced on 
        Kuwaiti investments in the US.<br>
        <br>
        Al-Hajri stressed that Kuwait was going strongly on reforms pertaining 
        to foreign investments. The government is framing legislations in this 
        regard — such as the issuance of foreign investment law which grants 
        customs exemptions of up to 10 years for foreign companies while 
        allowing projects ownership by 100 per cent for expatriates, in addition 
        to the new tax law which stipulates reduction of imposed taxes on 
        foreign companies from 55 per cent to 15 per cent. The law was presented 
        to the National Assembly for final approval. Al-Hajri also noted that 
        Kuwait had permitted foreign banks to operate in the country, besides 
        opening the insurance sector for foreign investment.<br>
        <br>
        Meanwhile, the US exports to Kuwait are estimated at nearly US$ 1.9 
        billion, while Kuwait’s exports to the US are estimated at nearly US$ 
        4.2 billion. Thus, the deficit in the trade balance, estimated at US$ 
        2.3 billion, is in favor of Kuwait. <br>
