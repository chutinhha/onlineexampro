        along with the Emirates Bank in Dubai may lend the supporting hand. In 
        December last year, His Highness Shaikh Mohammed bin Rashid Al Maktoum, 
        Vice President and Prime Minister of the UAE and Ruler of Dubai, had 
        ordered the privatisation of DFM through an IPO.<br>
        <b><br>
        Inflation, cause for concern</b><br>
        Two contradicting viewpoints are expressed on the mounting inflation in 
        the UAE. Standard Chartered Bank, in its recent report, expects the 
        inflation to climb up to 13.8 per cent this year from 10.4 per cent in 
        2005 and fall to 9.6 per cent in 2007. However, the government is 
        confident that inflation will fall to 4 per cent by the next year.<br>
        <br>
        The real estate market is the crux of the issue, and expected to 
        determine the inflation trend..With many real estate units likely to be 
        delivered by the year-end in Dubai, and by end-2007 in Abu Dhabi, says 
        Sultan Bin Nasser Al Suwaidi, Governor of the UAE Central Bank, the 
        inflation rate is expected to go down by the end of this year — and by 
        end-2007 is likely to be around four per cent, compared to eight per 
        cent for 2005. “Monetary policy alone cannot address inflation, as it 
        needs to be coordinated with fiscal policy,” points out the UAE Central 
        Bank Governor.<br>
        <br>
        On the other hand, the Standard Chartered Bank report says the upward 
        spiral of rents in Abu Dhabi and Sharjah will further accelerate 
        inflation to 13.8 per cent this year. Rent increases will contribute 
        over 50 per cent to the overall inflation in the UAE, according to the 
        report. Even after the supply of new houses, inflation is going to 
        remain around 10 per cent. Inflation in the UAE will remain high, says 
        this report, though the dynamics within the UAE will change.<br>
        <b><br>
        Consumer protection Law</b><br>
        As a part of the drive to control inflation and protect consumer 
        interest, the UAE government has issued a new law which will greatly 
        enhance consumer rights over buying, exchanging and returning of goods. 
        A new department will be created and it will become fully functional by 
        end-2006, according to the UAE’s Ministry of Economy undersecretary 
        Abdullah Al Saleh.<br>
        <br>
        The department will keep a close watch on price rises, attempt to wipe 
        out monopolies, and receive consumer complaints via a new hotline. 
        Consumer Protection Society, which was earlier dealing with consumer 
        problems, may take a new form once the law comes into force.<br>
