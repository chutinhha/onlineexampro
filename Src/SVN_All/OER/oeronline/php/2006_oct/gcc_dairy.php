            <font size="1" face="Verdana">Weekend switchover</font></b></p>
            <p align="justify" style="margin: 10px">
            <font face="Verdana" style="font-size: 11pt"><b>B</b></font><font size="1" face="Verdana">ahrain 
            and United Arab Emirates have adopted new Friday-Saturday weekend 
            structure from Septemeber 1. The switchover aims to bring the 
            countries in line with the working pattern of western countries. 
            While private institues and schools are yet to shift completely to 
            the new weekened regime, many private firms in the UAE are already 
            operating a Friday-Saturday weekend. The public sector and schools 
            have already adopeted the new structure.<br>
            Among the GCC countries, Qatar is already following the 
            Friday-Saturday weekend structure in official establishments. 
            However, a proposal was mooted recently in Qatar to shift the 
            weekend to Saturday and Sunday by aligning the weekend to the 
            international working week. <br>
            <b><br>
            Salaries get bigger</b><br>
            The rising cost of living, driven by inflationary pressure in the 
            Gulf region and tightening of government localisation rules, 
            continued economic growth in India and the weak US dollar has pushed 
            up the base salaries in the region. According to a survey by Dubai 
            based GulfTalent.com, the rate of salary increases was accelerating 
            in the UAE, Qatar and Kuwait, but slowing sharply in Saudi Arabia, 
            Bahrain and Oman. Gulf-wide wage growth averaged 7.9 per cent over 
            the 12-month period to August 2006.<br>
            <br>
            Qatar registered the highest average increase in pay at 11.1 per 
            cent, followed by the UAE at 10.3 per cent. Kuwait stood at 8.0 per 
            cent, close to the regional average. Saudi Arabia and Bahrain had 
            below-average increases of 6.5 per cent and 6.4 per cent 
            respectively, while Oman experienced the lowest increase at 5.6 per 
            cent.<br>
            The sectors that are experiencing fast growth and skill shortage in 
            the region have registered highest pay increases. The largest pay 
            rise was observed in the construction sector at 12.8 per cent, 
            followed by banking at 8.2 per cent, and oil &amp; gas at 7.7 per cent. 
            Sectors with the lowest rises were healthcare and education, at 4.5 
            per cent and 3.5 per cent respectively.<br>
            <br>
            The survey also indicated a clear shift in the salary structure 
            trends. Fewer companies are offering housing and transport, shifting 
            instead to a lump-sum cash payment. Performance-based variable pay 
            is assuming a bigger component of total pay packages, particularly 
            at the senior level. More companies are instituting formal pay 
            structures and annual salary review processes. Nationality-based pay 
            differentials still continue, but is narrowing. The UAE is probably 
            the only country where, in some cases, nationality-based pay 
            differences may be on the rise, driven by the government’s recent 
            push for Emiratisation of certain areas.<br>
            <br>
            Interestingly, more employers are considering female candidates for 
            positions that only a few years ago were considered the preserve of 
            men. The trend is particularly strong in Saudi Arabia, which has the 
            lowest female penetration in the workforce among GCC countries. A 
            female employee is preferred — not just because, on an average, 
            their cost is 10 per cent less than a male employee with similar 
            profile and performing a similar function — but also because 
            employers are now discovering that female employees provide them 
            with a strong competitive edge in serving the female segment of 
            their customer base.<br>
            <br>
            Governments across the Gulf continue tightening the rules on hiring 
            of expatriate workforce in the private sector. While the Saudi 
            market has long been subject to strict quotas, as have other Gulf 
            markets to a lesser degree, this year saw the relatively liberal UAE 
            market join the ranks — with the government beginning to impose and 
            enforce localisation targets for certain job functions in an effort 
            to eradicating unemployment among Emirati nationals. Given the 
            salary differential that exists between GCC nationals and 
            expatriates of similar profile, the increasing requirement to hire 
            more nationals is translating into higher average salaries and 
            increasing payroll costs for employers across the Gulf.<br>
            The upward pressure on compensations is expected to continue in the 
            future. However, it all will depend on what happens outside the 
            region — and the three prime factors are (a) the manufacturing boom 
            in China which has pushed up oil prices, (b) the economic growth 
            rate in India, and (c) the fate of the US dollar.</font></td>
