        All-in-one financial services regulator</b><br>
        The Kingdom of Bahrain has put in place an agency sanctioned to regulate 
        the entire financial services sector. To be sure, the sector comprises 
        of banking, insurance and capital markets.<br>
        <br>
        Bahrain Central Bank (CBB) assumed its authority on 7 September 2006 — 
        replacing Bahrain Monetary Agency (BMA) that was set up in 1973. In 
        2002, the government expanded BMA’s responsibilities to include 
        insurance and capital markets (notably Bahrain Stock Exchange) besides 
        banking. The new CBB Law encompasses all the different segments of 
        financial services sector into a single document. Needless to say, CBB 
        has assumed all legal rights of BMA, including those of issuing currency 
        and notes.<br>
        Reflecting the significance of the institu<br>
        tion, CBB has a paid up capital of BD200m (US$529m), with an authorised 
        capital of BD500m. This is a sizeable amount for a small economy. 
        Bahrain’s GDP at constant prices, adjusted for inflation, was BD3.5bn 
        (US$9.3bn) in 2005.<br>
        <b><br>
        <img border="0" src="bahraina.jpg" width="200" height="181" align="left" hspace="5" vspace="2">Expanded scope</b><br>
        Aside from seeing the arrival of CBB, Bahrain’s banking industry 
        underwent another development in the third quarter of 2006. As of July, 
        the then BMA implemented a new framework for financial institutions 
        operating in the country on the basis of retail and wholesale banking. 
        As such, the very concept of offshore banking units (OBUs) was scrapped. 
        Instead, former OBUs were asked to seek licenses based on the nature of 
        their banking activities, allowing them to combine various elements 
        concurrently. Technology and globalisation had rendered the notion of 
        offshore banking obsolete. <br>
        <br>
        The change fits the country’s overall strategy of strengthening the role 
        of financial services sector into the local economy, and for good 
        reasons. In fact, the sector is the leading contributor to Bahrain’s 
        GDP. It was worth BD966m (US$2.5 bn), thereby accounting to nearly 28 
        per cent of the GDP (in constant terms) in 2005. This is a record, and 
        represents a hefty 17 per cent growth vis-à-vis the 2004 figures. <br>
        <b><br>
        Critical charges </b><br>
        While welcoming the establishment of CBB, critics have questioned the 
        timing of the new institution. In the next few years, Bahrain might have 
        to give up central banking authority to a GCC-wide structure. If all 
        goes according to plan, the six-nation grouping is scheduled to form a 
        unified monetary policy — culminating into a single currency by 2010. 
        Accordingly, a new pan-GCC central banking authority would come into 
        existence.<br>
        <br>
        On the other hand, some critics are not pleased with lack of full 
        independence to the CBB. The law stipulates that CBB is responsible 
        before a cabinet minister. Accordingly, CBB officials are not completely 
        independent with respect to making decisions regarding setting of 
        interest rates and supervision, as well as permits. In any case, CBB 
        does not enjoy the privilege of influencing interest rates by virtue of 
        the Bahraini dinar being pegged to the US dollar.<br>
        <br>
        Clearly, by setting up a new central banking authority and changing 
        framework of licenses, Bahrain is determined to protect its reputation 
        as the primary banking hub of the region. Undoubtedly, Bahrain is 
        encountering rivalry from Dubai International Financial Centre (DIFC) 
        and other new financial centres in Qatar and Saudi Arabia. However, it 
        is the US$2bn DIFC that is posing the most immediate serious threat to 
        Bahrain’s position as the premier base for financial services in the 
        region. <br>
        <br>
        Only time will tell whether setting up of a new central banking 
        authority in Bahrain without full independence was the right move in the 
        face of growing competition in the region.<br>
