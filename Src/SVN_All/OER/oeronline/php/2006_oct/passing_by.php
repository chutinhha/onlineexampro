<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><font face="Verdana" style="font-size: 14pt"><b>Music is the common 
language of mankind<br>
</b></font><font face="Verdana" size="1"><b><br>
Yamaha’s origin and heritage is in the musical instruments business. Since 
manufacturing its first organ more than a century ago, Yamaha has grown into the 
acknowledged leading maker of musical instruments in the world. Shigeru Hikida, 
Commercial Director, Yamaha Music Gulf FZE, aims to take his company to soaring 
heights in the Middle East. Mehre Alam caught up with him during his recent 
visit to Oman. Excerpts of the interview:</b><br>
<b><br>
<img border="0" src="passby.jpg" align="left" hspace="5" vspace="2">How 
big is the musical instruments market in the Middle East?</b><br>
The UAE, especially Dubai, continues to remain a big market for musical 
instruments business. But Dubai is known primarily for its huge re-exports 
trade. We are looking to set our feet firmly in a country like Oman, where 
people are very keen to pursue music education. The Omani government is very 
conscious of promoting its culture and music.<br>
<br>
For us, Oman has shown the way when it comes to developing and promoting 
institutional music education. So I think Oman offers us a very good environment 
for the business to grow. However, there are pockets in the Middle East where 
it’s still very difficult to nurture and promote the musical instruments 
business.<br>
<b><br>
Is your company planning to ink a super audio system project with the Omani 
government?</b><br>
The key components of our business are musical instrument, home audio and the 
professional audio system. It’s the professional audio system where we are 
looking to ink a deal with the government here. In the offing is a big project 
in super audio system installation. We are partnering Genetco (General Electric 
&amp; Trading Co. LLC), for the Omani project — a music hall, or some such kind of 
thing. <br>
<br>
Our digital mixing sound system is unique and has made its mark at international 
events like the Olympics opening and closing ceremonies.<br>
<b><br>
What’s special about this project?</b><br>
Oman, for the first time, will have a super audio system — the quality of which 
it is hitherto not used to. It is going to be a breakthrough technology as far 
as Oman is concerned. The same equipment that were used at the Athens Olympics 
as well as the recent FIFA World Cup will be — for the first time — paraded in 
Oman, and expected to become a part of events like Muscat Festival. Once the 
system comes in place, huge stadia can be serviced from within Oman itself.<br>
<br>
The kind of audio that Oman is used to so far is nowhere near world quality. But 
we know that Oman can afford this. The ministries in Oman can afford this. The 
state-of-the-art equipment will make a huge difference. Genetco is going to 
invest in this system on its own so that the authorities here can take notice, 
and feel the difference between what they have been using and what can be used; 
the quality that they are used to and the quality that can be had. For the 
amount that these institutions are paying, I think they deserve this kind of 
equipment.</font></p>
