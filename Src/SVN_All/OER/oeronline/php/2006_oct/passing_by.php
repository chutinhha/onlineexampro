<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Is it going to be a first in the Middle East?</b><br>
It will indeed be a first for Oman but not for the Middle East. Two broadcasting 
corporations in the Middle East are using this state-of-the-art equipment. One 
is LBCI (Lebanese Broadcasting Corporation International, a leading Lebanese TV 
station, and the other is Future TV. In fact, the radio station in Beirut that 
was bombed in the recent attacks by Israel was using one of the best Yamaha 
products. <br>
<br>
As for Dubai, unfortunately, none of the broadcasting stations there are using 
our equipment. Dubai has lots of world-class skyscrapers -- but acoustically, 
the city is really not that tall! There are some nice halls with good view, but 
when it comes to acoustics, they all fall much short of world standards. In 
Oman, you have a few concert halls which are much better acoustically compared 
to Dubai.<br>
<b><br>
Which market in the Middle East do you plan to focus on?</b><br>
Ideally, we would like to expand throughout the Middle East — but Oman is one 
market which is quite conscious about quality acoustics. It’s the only country 
in this region that has a professional orchestra. From the point of view of 
business, Oman is indeed a very special market for us.<br>
<b><br>
Does Yamaha’s rate of growth in Oman justify your expectations?</b><br>
As far as Oman is concerned, Yamaha’s sales (from 2004) have gone up by about 60 
per cent in 2005 and are about to register another 50 per cent jump in 2006. We 
are practically aiming at another 50 per cent jump in 2007. So the rate of 
growth of business for Yamaha has been approximately between 50 and 60 per cent 
during the last three years.<br>
<b><br>
Which are the sectors that have witnessed a greater demand for Yamaha products?</b><br>
It has been an overall growth. Every segment of our business has grown. 
Institutional sale has gone up in 2006. We had not been doing much business with 
the Omani institutions in the previous years. This year, for example, the Arabic 
keyboards have all come from Yamaha. In fact, we are supplying 600 of such 
keyboards to the institutions like schools. This has been a big breakthrough. We 
are also standardising equipment for use in classrooms as well as in 
professional studios.<br>
<b><br>
How big an impact the deal on super audio system is likely to make? </b><br>
If things really catch up we should grow by 100 per cent. But then that’s a 
dream. If you have just started and can still hit the tree, you should be very 
happy. <br>
<b><br>
What’s the vision for your company in the Middle East?</b><br>
We consider music as the common language of mankind. We would like to put 
emphasis on music education.</font></p>

