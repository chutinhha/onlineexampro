<font face="Verdana" size="1"><b><br>
OBG’s new Country Director</b><br>
Oxford Business Group (OBG), the UK-based publishing, research and consultancy 
service, has appointed Bébhinn Kelly as Country Director for Oman. Kelly will 
collaborate with OBG’s official research partners in the production of Emerging 
Oman 2007, part of OBG’s internationally acclaimed Emerging Market Series. OBG 
is currently based with the Omani Centre for Investment Promotion and Export 
Development (OCIPED), their national strategic partner, for the third year 
running.<br>
<br>
Specialising in Intercultural Business, Kelly graduated with an honours degree 
from Dublin City University in Ireland. Previously in Qatar with OBG, she will 
now be based in Muscat.<br>
