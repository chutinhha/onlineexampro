<font face="Verdana" size="1"><b>Oman’s balanced growth praised</b><br>
In the wake of the good results achieved in the Seventh Five-Year Development 
Plan in various fields, His Highness Sayyid Fahd bin Mahmoud Al Said, deputy 
prime minister for the Council of Ministers, has stated that the average 
economic and social growth in the Sultanate is the pride of all Omani people. 
The projects planned for current five-year development plan and future plans 
will meet the basic needs of Omani citizens, he added. He said that the 
decentralised policy followed by the Sultanate in planning has created a 
balanced growth in the country. In a statement to the Oman News Agency, Sayyid 
Fahd said Oman has started many ambitious projects, which will reflect 
positively on all people. He said that in the comprehensive development march, 
the social sector is receiving the personal care and attention of His Majesty 
Sultan Qaboos bin Said. He said under His Majesty’s wise directives, the Council 
of Ministers is working towards translating all these projects into reality. 
Sayyid Fahd said the development plans are focusing on the wellbeing and 
prosperity of the Omani people. He also spoke about the good reputation the 
Sultanate is enjoying in the international arenas. International organisations 
are commending the Sultanate for the success of its development and the positive 
contributions of the citizens and the government to the comprehensive 
development march. He said that the Omani citizen is achieving continual success 
for his country, in the wake of the freedom and rights granted to him by the law 
and society.<br>
