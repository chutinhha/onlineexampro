<font face="Verdana" size="1"><b>Oger Telecom joins DIFC</b><br>
Oger Telecom, a leading telecommunications, multimedia and holding company, with 
approximately 30 million subscribers under management, has established its 
global headquarters at the Dubai International Financial Centre (DIFC).<br>
<br>
Oger Telecom is an Emerging Markets Telecommunications holding company. The 
majority shareholder is Saudi Oger. As an international telecommunications 
operator, the DIFC provides Oger Telecom with a world-standard operating and 
legal environment, zero tax rate, and proximity to advice and expertise from a 
vibrant community of global financial institutions. <br>
<br>
Joe Haj Ali, the Chief Marketing &amp; Communications Officer and the General 
Manager of Oger Telecom Dubai, said: “Oger Telecom is very pleased to be 
positioned at the DIFC, as it provides an efficient platform from which to 
operate throughout the region and is further evidence of our continued 
commitment to support the growth and expansion of our business interests in the 
Middle East.” Speaking on behalf of the Dubai International Finance Centre, 
Assem O. Kabesh, Chief Business Development Officer and Board Member of the 
DIFC, said: “Oger Telecom will benefit greatly from proximity to the DIFC’s 
wealth of financial services talent, drawn from the world’s leading 
institutions.”<br>
