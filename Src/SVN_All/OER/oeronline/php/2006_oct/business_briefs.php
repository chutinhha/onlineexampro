<font face="Verdana" size="1"><b><br>
HSBC injects $18 m into its Oman branch</b><br>
HSBC Bank Middle East Limited, Oman, recently received a US$18million increase 
in its capital from its Head Office in Jersey, Channel Islands. This raises the 
capital to RO18.7million, an increase of RO7 million. The Central Bank of Oman 
requires foreign banks to have a minimum capital of RO10 million in place by end 
of 2008. The total capital and reserves of the HSBC in Oman has now reached RO40 
million. “The increase in capital will allow us to continue our strong growth in 
our corporate, commercial and business banking, and personal finance business,” 
said Richard Groves, HSBC Oman’s Chief Executive Officer. <br>
Meanwhile, in an exclusive presentation for HSBC’s corporate customers in 
Muscat, David Bloom, Global Head of Foreign Exchange Strategy, HSBC Global 
Markets (based at the Group headquarters in London) outlined the Group’s 
currency outlook for the coming year.<br>
