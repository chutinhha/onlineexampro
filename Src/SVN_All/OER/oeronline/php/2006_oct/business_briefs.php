<font face="Verdana" size="1"><b>New digital signage solution</b><br>
The projects and engineering division of Omasco, the authorised distributors of 
Panasonic products in Oman, has succeeded in implementing digital signage 
solution at the Oman International Exhibition centre. Oman International 
exhibition centre has adopted this solution for their exhibition and event 
marketing activities involving the Panasonic plasma displays. The digital 
signage solution provided incorporated series of hardware and software modules 
like Signage designer station, network manager software, players software, slim 
PCs and 12 numbers of Panasonic 42” standard definition plasma displays 
connected to the dedicated LAN infrastructure. With this solution Oman 
exhibition centre will have complete control over their network and will be able 
to schedule their marketing programmes on a daily or even hourly basis, through 
the central location. Digital signage system is the latest in communication and 
advertising technology that incorporates multimedia technology to rapidly change 
content, accept current advertising media (still images/video footage), 
dynamically change content and enhance content provision with full multimedia 
capability. The digital signage solution can be implemented in different 
segments like retail, corporate, government and the hospitality sectors.<br>
