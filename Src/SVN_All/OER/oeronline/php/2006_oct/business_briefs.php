<font face="Verdana" size="1"><b>NBO initiative to enhance corporate 
customer-base</b><br>
In a bid to enhance its corporate customer-base, Oman’s second largest 
commercial bank, National Bank Of Oman (NBO) has announced the opening of its 
corporate contact centre at its head office at Ruwi. The move is as part of the 
bank’s ongoing initiatives to offer additional and value added services to 
thecustomers. NBO is the first bank in the Sultanate to open an excusive 
corporate contact centre for its corporate customers. The centre will handhold 
corporates to ensure that the services are delivered in a very effective and 
efficient manner involving bare-minimum turn-around time. <br>
<br>
In addition, the contact centre will, inter alia, offer a wide array of services 
to corporates such as collecting cheques, bills, documents, LC/LG applications 
at doorsteps, and also handle all inquiries relating to accounts, products, 
services and transactions. <br>
<br>
“Our focus is always on customer satisfaction, and our initiatives are aimed at 
improving customer service and adopting best business practices. We expect this 
year and the future years provide continued opportunities for us to deliver more 
value-added services to our customers and enhance stakeholder-value,” says NBO’s 
deputy CEO Abdulaziz Mohammed Al Balushi. <br>
