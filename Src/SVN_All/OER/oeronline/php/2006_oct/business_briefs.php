<font face="Verdana" size="1"><b><br>
Nawras donation drive</b><br>
Nawras launched the humanitarian SMS campaign under the theme “Help the people 
of Lebanon” by donating 209 baiza per SMS. In order to make the donation to the 
people of Lebanon possible, Nawras joined hands with its strategic partner — 
Oman Charitable Organisation (OCO). At a ceremony organised at the Nawras main 
office at Al Athaiba, Saleh Al Riyami, Nawras Board Member and Khalid Al 
Mahmoud, Nawras COO, handed over the donation to HE Sayyid Mohammed bin Sultan 
Al-Busaidi, Under Secretary at the Ministry of Interior and Vice Chairman of the 
OCO and other Nawras senior management.<br>
<br>
As part of a separate initiative, Nawras was the first mobile operator in Oman 
to introduce a customer rewards programme, initially beginning with 16 partners 
and approximately 30 outlets in October 2005. Aiming to provide extra value to 
its customers, Nawras has set up partnerships with companies all over Muscat and 
throughout the Sultanate. <br>
