<font face="Verdana" size="1"><b>Economist Roundtable </b><br>
Economist Conferences, a division of the Economic Intelligence Unit, held the 
first Business Roundtable with the Government of Oman. The basic agenda of the 
Roundtable was to review the progress of the Sultanate. The issues of debate 
were: Can Oman forge a competitive edge sharp enough to contend with regional 
neighbours including UAE, Qatar and Bahrain, and whether the drive towards 
Omanisation of the labour force is going to block progress for international 
corporations? The meeting also discussed how the private sector could help Oman 
break away from its dependence on oil, and what action was required to exploit 
free trade opportunities with the US? The Roundtable featured prominent speakers 
like Delia Meth-Cohn, Editorial Director, Economic Intelligence Unit CEEMEA, HE 
Maqbool bin Ali bin Sultan, Minister of Commerce and Industry, Jeroen van der 
veer, Chief Executive, Royal Dutch Shell plc, Ahmed Abubaker Janahi, Chairman 
and CEO, AAJ Holdings Company B.S.C and James Hogan, President and Chief 
Executive, Gulf Air.<br>
