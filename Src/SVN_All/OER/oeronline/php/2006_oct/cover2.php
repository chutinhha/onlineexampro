            <font face="Verdana" style="font-size: 14pt"><b>The rental leap</b></font><font face="Verdana" size="1"><b><br>
            Rents in Muscat areas have leapfrogged by at least 20-25 per cent in 
            the last one year — and they are expected to harden further, finds 
            Mehre Alam</b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he ground slipped fast under his feet. Much faster than he had 
            imagined. When A. Jawed, a senior executive working in Muscat, went 
            home on summer vacation this year, he had just about postponed his 
            house-shifting plans. He wanted to move closer to office. The CBD 
            (Central Business District) area in Muscat is what he had earmarked. 
            And there were plenty of signboards soliciting tenants. It was on 
            returning back to Muscat that he got the bolt from the blue. There 
            were hardly any houses available in the CBD area — and the few, 
            located after much sweating in the sun, were all quoting much higher 
            figures for rents.<br>
            <br>
            Welcome to Muscat. A city leapfrogging when it comes to rentals!<br>
            M. Sudhakar Reddy, General Manager, Al-Habib &amp; Co., a leading real 
            estate company, agrees that things have changed thick and fast in 
            the past few months. “Occupancy rates have touched 98-99 per cent in 
            the last few months. There were instances when we had to return our 
            clients empty-handed, as we didn’t have anything available to suit 
            their requirements. It’s a fact that there is a shortage of property 
            right now, thanks to the heightened demand,” he says.<br>
            <br>
            The rentals have been going up steadily in Muscat. “Rentals had been 
            stable for many years and it started moving up sometime last year. 
            In the last one-year period, we have seen an increase of 20-25 per 
            cent. The increase in rentals for new properties are higher compared 
            to the old properties, which also have shot up by 10 per cent,” 
            informs Reddy.<br>
            A look at the rental figures of the last two years comes as an 
            eye-opener. (see infograph). <br>
            <br>
            The reasons for this are not hard to see. It’s a clear-cut 
            demand-supply game. The number of employees, both Omanis and 
            expatriates, being hired by companies has been going up. But the 
            supply of houses cannot increase drastically. In fact, it takes a 
            couple of years for an apartment block to be constructed and for the 
            tenants to move in.<br>
            Says Reddy: “We expect this situation to continue for at least the 
            next couple of years. We know it’s a period of booming construction 
            but there is an additional demand of more than, 2,500 apartments a 
            year in Muscat alone. That’s about 5 per cent of the present stock. 
            Now, manpower has been growing at 5 per cent too. Therefore, an 
            increase of 5-6 per cent is required in housing stock too. But it’s 
            not easy to build 5-6 per cent of the present stock. After all, this 
            present stock has been built over 30 years.”<br>
            In terms of rents, which are the areas more in demand for 
            flats/houses? Informs Reddy: “The city is shifting towards Seeb. The 
            process has picked up momentum now. The fastest growing areas in 
            Muscat today are Al Khuwair, Azaiba, Al Ghubra and Bousher. These 
            are the areas that are growing the fastest, and these are the areas 
            that are most in demand.”<br>
            <br>
            Is Muscat going the Dubai way? “No, nor the Doha way, by our 
            reckoning. We don’t see rents doubling or tripling or anything of 
            the sort,” asserts Reddy. However, he pops out a qualifier: “But we 
            expect rents to continue to harden.” There lies the punch.</font></p>
