                    </font><font face="Verdana" size="4" color="#FF0000">On firm 
            footing</font><font face="Verdana" size="1"><br>
            <br>
            <b>The prospects for the Omani economy look good for the year 2005, 
            where we are expecting the economy to realise a growth rate of the 
            GDP of more than 9 per cent in nominal terms, predicts: <br>
            <br>
            </b>
            <img border="0" src="im3.jpg" align="left" width="150" height="168"><b>His 
            Excellency Ahmed Bin Abdulnabi Macki <br>
            Minister of National Economy &amp; Vice President of The Financial 
            Affairs and Energy Resources Council</b></font></p>
            <p align="justify"><font face="Verdana" size="1">The Sultanate of 
            Oman’s political, financial and monetary stability, its free and 
            open economy, vast and diversified natural resources, especially 
            natural gas, and important tourism potential enable it to lay the 
            foundations of a solid economy and to play a distinct regional role. 
            The introduction of appropriate measures and legislative changes 
            made the economy more competitive and encouraging to the private 
            sector and foreign investment. <br>
            <br>
            The investment characterised by flexible investment and tax regimes 
            allowing for free repatriation of capital and profits, modern 
            infrastructures, industrial zones equipped with the necessary 
            facilities and infrastructures created a very conducive environment 
            for foreign investors. All these factors combined enabled Oman, 
            under the enlightened leadership of His Majesty Sultan Qaboos, to 
            pursue its economic and social development policies with vigour, 
            confidence and dynamism.<br>
            <br>
            The economy has gradually reduced its level of dependence on the oil 
            sector. From about 32 per cent share of GDP, a mere 4 per cent 
            contribution in government revenue and almost less than 1 per cent 
            of our export earnings in 1970, the non-oil sectors contribute 
            presently to about 61 per cent of GDP, over 25 per cent of 
            government revenue and about 30 per cent of the total export 
            proceeds. <br>
            <br>
            The country witnessed noticeable economic and social achievements: a 
            modern physical infrastructure, considerably improved basic health 
            and education systems and the overall standards of living of Omanis, 
            and impressive development in GDP and GDP per capita. Oman was thus 
            transformed, in about three decades, from an underdeveloped 
            subsistence economy to an open, free, modern and dynamic economy.<br>
            <br>
            Aware of Oman’s relatively limited crude oil resources, its 
            increasing population and the vulnerabilities associated with the 
            over dependence on oil, the government’s long-term objectives were 
            centred around reducing dependency on oil and diversifying the 
            economic base of the country. <br>
            <br>
            Economic policies were, thus, designed to reshape the role of the 
            government and to lead Oman on the private-sector-led economic 
            development path, with a firm commitment to fiscal stability and 
            sustainability, HRD and capacity building, continuation and 
            promotion of the privatisation process, diversification of the 
            economy, creating employment opportunities to a fast growing young 
            population, increasing the level and rate of private savings and 
            encouraging private local and foreign investments.<br>
            <br>
            In pursuance of these objectives and policies, the government has 
            exerted a lot of efforts in the past few years on various fronts. A 
            major concern was to exploit the country’s important natural gas 
            resources. This has stimulated the diversification drive based on 
            the development of gas-based industries and the expansion of the 
            services sector. Hence, several important projects have been 
            completed. The LNG project is being expanded with the addition of a 
            third train, raising the capacity to 10 mtpa from 6.6 mtpa. <br>
            <br>
            Liquefied natural gas is set become the country’s major non-oil 
            earner and is expected to generate $24 billion over the coming 25 
            years. The government has invested heavily in developing the gas 
            fields, pipelines to Sohar and Salalah. In addition, a new round of 
            major new upstream gas projects are being pursued: a new 20 million 
            cubic metre per day gas treatment plant, the Oman-India fertiliser 
            project, a world scale polypropylene project using feedstock from 
            the planned Sohar Refinery, a gas-based polyethylene project and 
            downstream processing of polyethylene, as well as the aluminium 
            smelter plant of a capacity of up to 630,000 tonnes per annum.<br>
            <br>
            On the other hand, despite the declining oil production, the 
            strength of the non-oil sector and the high oil prices ensured a 
            strong macroeconomic performance of the economy where it realised a 
            GDP growth rate of about 6 per cent in 2003. In spite of the fall in 
            crude oil production and the fluctuations of oil prices, total 
            exports remained stable to about RO4 billion during the past few 
            years, thanks to the increased LNG and non-oil exports proceeds. <br>
            <br>
            The fiscal position is also very good where the overall fiscal 
            balance averaged about 6 per cent of GDP during 2001-2003. The 
            external current account position also remained firm, the surplus 
            averaging about 8.5 per cent of GDP during the same period. The 
            monetary policy is successfully geared to realise its objectives: 
            preserving a stable exchange rate, maintaining price stability and 
            enhancing the soundness of the financial system. The soundness of 
            the financial system is reflected in the well capitalisation and 
            provisioning of the commercial banks, as well as our known 
            compliance with the international standards and codes for financial 
            stability.<br>
            <br>
            In a nutshell, then, although Oman is a small open economy, it was 
            able to realise the remarkable achievements and progress outlined 
            above, due mainly to its prudent mix of fiscal and monetary policies 
            and its commitment to structural reforms to support the 
            diversification strategy and the privatisation programs, and to 
            enhance the competitiveness of the economy and the role of the 
            private sector.<br>
            <br>
            <b>Future outlook</b><br>
            The prospects for the Omani economy look good for the year 2005, 
            where we are expecting the economy to realise a growth rate of the 
            GDP of more than 9 per cent in nominal terms, and in the subsequent 
            years, albeit certain difficulties associated with the decline in 
            oil output in recent years and the volatile nature of the oil 
            market. <br>
            <br>
            The reasons for this optimism emanate from a number of factors. 
            These are the current high oil prices, which are expected to remain 
            firm during the short to medium term, moderation in the decline of 
            oil production in Oman, the mounting importance of the gas sector, 
            the strong demand due to increased oil receipts, and the strong 
            growth of the non-hydrocarbon sector.<br>
            <br>
            The strong non-hydrocarbon economic growth over the medium term 
            emanates from the rising trend of government and private sector 
            investments in the ongoing and planned industrial and services 
            projects, and a buoyant domestic demand. All these are expected to 
            enable the non-oil sector to realise a real growth rate of about 5.5 
            per cent in 2004 and about 5 per cent in subsequent years, bringing 
            the average real growth of the economy to about 5 per cent in the 
            coming three years. <br>
            <br>
            On the other hand, the gas sector is becoming increasingly important 
            to the economy of Oman and its development is proceeding rapidly. 
            Total gas demand is expected to more than double by 2010, from the 
            current level of approximately 40 million cubic meters per day. 
            Conversely, a major drive has already been initiated to increase oil 
            output and a series of new large water flood projects and enhanced 
            oil recovery projects are in development. These projects are 
            expected to add substantial new oil production. <br>
            <br>
            In addition, oil exploration continues with discoveries in the 
            north, central and southern Oman. It is hoped that with these 
            measures, oil production will start to pick up gradually and that 
            historical production levels could be realised sometime by the year 
            2009. Furthermore, the government is committed to pursue a prudent 
            and realistic fiscal policy centred on the strengthening of fiscal 
            institution, rationalisation of government expenditure and enhancing 
            non-oil revenue. <br>
            <br>
            The 2005 budget will, thus, be guided by prudence in estimating 
            revenues and rationalisation of expenditure, maintaining fiscal 
            sustainability, building government reserves, keeping foreign debt 
            at manageable internationally acceptable levels, promoting youth 
            employment programs while, at the same time, keeping the fiscal 
            balance under control.<br>
            <br>
            On the other hand, the important steps that have been taken in the 
            past few years to promote the stability of the financial sector, to 
            open up the economy for foreign investments and to strengthening 
            human capital development will be enhanced and consolidated to 
            achieve sufficient job opportunities for a rapidly growing local 
            labour force, and to pursue the diversification policy. In the 
            regulatory framework, the Sultanate has already made big strides and 
            laid down flexible investment and tax regimes, to attract foreign 
            investments, allowing for free repatriation of capital and profits.<br>
            <br>
            Last, but not least, is the area of privatisation, where Oman is 
            known to be the leading country in the Gulf in this respect. Plans 
            are progressing well and according to schedule, especially in the 
            areas of electricity and communication. The important developments 
            in privatisation initiatives in the past two years included the 
            power, telecommunication and water sectors, as well as wastewater 
            projects, postal services and surface transport.<br>
            <br>
            All the above indicate that future economic prospects of the economy 
            are promising. The availability of a modern infrastructure, 
            constantly upgraded, the very conducive institutional, legal and 
            regulatory framework make Oman an attractive and safe haven for both 
            local and foreign investments, especially in gas-based industries, 
            IT and tourism. <br>
            <br>
            Furthermore, Oman enjoys a recognisable international rating in 
            risk, and is well known for its credit worthiness and ability to 
            meet its financial obligations. Although the economy has to deal 
            with a lot of difficulties and challenges, domestically and 
            internationally, it is yet pursuing its objectives with 
            determination, vigour, confidence and dynamism.</font></p>
