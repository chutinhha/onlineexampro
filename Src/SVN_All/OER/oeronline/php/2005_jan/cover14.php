            Director (Strategy), Global Markets, HSBC Bank, London</font></b></p>
            <p align="justify"><font face="Verdana" size="1">The global economy 
            forecast could be summed up in one sentence: Despite the higher oil 
            prices, inflationary pressures are remarkably low, and with global 
            growth slowing into 2005, central banks may reverse monetary 
            tightening next year. Although oil prices have continued to rise, 
            their impact on inflation has been remarkably small. After some 
            initial worries earlier this year, inflationary pressures have 
            quickly faded, and the latest inflationary trends are as weak as 
            they were in 2003, when central banks were fretting about deflation.<br>
            <br>
            The absence of any feed-through from higher oil prices to inflation 
            is explained by a mixture of four different factors. First, in the 
            western world, energy dependency is less than before. Second, with 
            credible central banks and with competitive global markets, 
            companies simply don’t have pricing power. Third, companies may 
            still be suffering from excess capacity in a post-bubble world. 
            Fourth, the integration of China and India into the global economy 
            may be exerting deflationary pressures elsewhere, notably through a 
            squeeze on labour incomes.<br>
            <b><br>
            Growth is slowing</b><br>
            The soft-patch that has been mostly attributed to the US economy in 
            the middle of the year appears to have been a lot more widespread. 
            Our measure of global GDP growth slowed significantly in the second 
            quarter of the year, as domestic demand growth softened in many 
            parts of the world, in some cases more than in the US. For some of 
            these countries—notably Japan and some members of the eurozone—the 
            softening of domestic demand is probably linked to weaker export 
            growth: there is little evidence to date of self-sustaining domestic 
            economic momentum. For most countries, we suspect that higher oil 
            prices have had an impact not so much through inflation but via 
            deteriorating terms of trade. And, for those exposed to China, 
            Beijing's attempts to slow Chinese domestic demand may be having a 
            negative effect on exports of capital goods.<br>
            <br>
            <b>The policy conundrum</b><br>
            Low inflation and slower growth are hardly the typical drivers of 
            tighter monetary policy yet, for the time being, we expect the 
            Federal Reserve to continue raising interest rates. Although 
            ‘‘neutrality’’ is a vague concept, the Fed clearly believes that Fed 
            funds today are still some way below an appropriate ‘‘neutral’’ 
            rate. We expect monetary tightening to continue through to the 
            second quarter of next year. Thereafter, however, as concerns shift 
            away from cyclical inflation towards structural deflation, and as 
            the housing-induced leverage effect on consumers begins to unwind, 
            we expect interest rates to come down once again, placing the dollar 
            under more downward pressure against the euro.<br>
            <br>
            <b>Inflation insight</b><br>
            Views on the global economy appear to be oscillating between 
            “cyclical inflation” and “structural deflation”. Earlier in the 
            year, the “cyclical inflation” view was dominant. At the time of 
            writing, the “structural deflation” view was in the ascendancy. <br>
            <br>
            Over the last quarter, two main surprises have emerged. First, 
            longer-term bond yields have fallen despite moves towards tighter 
            monetary policy in many parts of the world. Second, inflation has 
            been remarkably quiescent, particularly given the continued upward 
            pressure on oil prices.<br>
            <br>
            We explore the reasons that lie behind the lack of inflationary 
            pressures. Energy is a much lower share of output in the Western 
            world than in the 1970s, suggesting less of a feed-through into 
            inflation. Competitive markets suggest that the old “cost plus 
            mark-up” model of inflation is no longer valid: instead, higher oil 
            prices imply either a squeeze in margins or a reduction in labour 
            costs. In a post-bubble world, excess capacity weighs on pricing 
            power. Finally, the beneficial supply-side shock coming from the 
            integration of China and India into the global economy may have 
            lowered prices on a structural basis for the Western world.<br>
            <br>
            The absence of inflation creates new uncertainties for central 
            banks. Should they continue to raise rates, confident in the 
            apparent recovery in economic activity? Or, instead, should they 
            keep rates on hold, recognising that recent trends point to the same 
            deflationary pressures that were uppermost in policy-makers’ minds 
            last year?<br>
            <br>
            For the time being, the Federal Reserve is likely to stick to its 
            belief in cyclical recovery, and hence remain committed to returning 
            Fed funds to “neutrality”. We expect US official rates to rise to 3 
            per cent in the second quarter of 2005. Thereafter, however, we 
            expect rates to fall as the US economy falls victim to excessive 
            leverage.<br>
            <br>
            Meanwhile the global economic cycle appears to be running out of 
            steam. The evidence from the second quarter suggests that domestic 
            demand weakened in many different parts of the world. Although the 
            US “soft patch” has been blamed for weakness elsewhere, we suspect 
            that other factors have also been involved. Higher oil prices have 
            been unhelpful. China’s slowdown may be beginning to bite. And, in 
            some countries, the slow withdrawal of the previous policy stimulus 
            may be placing a brake on domestic economic expansion.<br>
            <br>
            Overall, although the global economy may continue to expand in the 
            short-term, post-bubble vulnerabilities still exist, particularly in 
            the US. America’s solution in 2005 is likely to involve a further 
            depreciation of the dollar against the euro, as domestic growth 
            fades from view. Global growth may have been strong in recent 
            quarters but we doubt that this strength will be maintained.<br>
            <br>
            <b>Conclusions</b><br>
            • Over the last quarter, inflation has been remarkably quiescent 
            despite the continued rise in oil prices. The dilemma for central 
            banks is whether to continue to raise interest rates, confident in 
            the apparent recovery in economic activity or whether to keep rates 
            on hold, recognising that structural deflationary pressures persist.<br>
            • For the time being, the Federal Reserve is likely to stick to its 
            belief in cyclical recovery, and hence remain committed to returning 
            Fed funds to “neutrality”. We expect US official rates to rise to 3% 
            and 10-year yields to hit 5% by mid-2005.<br>
            • Thereafter, however, we expect rates to fall as the US economy 
            falls victim to excessive leverage, placing the dollar under more 
            downward pressure against the euro. Our long-held view that we see 
            sub-4 per cent bond yields and the dollar at 1.35 Vs the euro within 
            the next 18 months therefore remains unchanged.<br>
            • In the eurozone recent comments from ECB members, including 
            President Trichet, suggest the ECB has become more hawkish. 
            Nonetheless we remain of the view that growth concerns will keep the 
            Bank on hold in the coming months with the result that the next move 
            in rates—in second half of 2005—is likely to be down. Policy is also 
            likely to remain unchanged for some time in Japan.<br>
            • Overall, although the global economy may continue to expand in the 
            short-term, post-bubble vulnerabilities still exist, particularly in 
            the US. Together with further tightening measures coming through in 
            China to slow the investment boom, this suggests that the recent 
            strength of global growth will not be maintained.<br>
            • The world trade cycle has already displayed the initial signs of 
            weakening. With growth in the US and China set to slow, and growth 
            in much of the rest of the world heavily dependent of exports as the 
            major driver, a synchronised deceleration appears to be on the cards 
            next year. We expect global GDP growth to slow from an average of 
            4.7 per cent in 2004 to 3.7 per cent in 2005.</font></p>
