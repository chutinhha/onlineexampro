            <p><b><font face="Verdana" size="1">Although the global economy may 
            continue to expand in the short-term, the recent strength of global 
            growth will not be maintained, warns:<br>
            <br>
            <img border="0" src="im16.jpg" align="left" hspace="6" width="150" height="157">David 
