            <p align="justify"><font face="Verdana" size="1">Till about a few 
            years ago, Oman’s telecommunications market was not as developed as 
            some of the other countries in the GCC. However, that situation is 
            fast changing today, with the Sultanate’s liberal telecom regime and 
            access to leading-edge technology attracting multinational telecom 
            operators, service providers and manufacturers.<br>
            <br>
            In 2003, the government formed the Telecom Regulatory Authority (TRA) 
            to set the framework for a competitive market for telecom services 
            by abolishing Oman Telecommunication Company’s (Omantel) exclusive 
            right to provide services. In June 2004, the market was opened up 
            and Nawras Telecom, a consortium led by the Danish company TDC and 
            Qatar’s Q-Tel along with Omani partners, was awarded a second public 
            mobile licence. This ended the newly formed Oman Mobile’s monopoly 
            in the Sultanate.<br>
            The Sultanate enjoys a large geographical area of land. The case in 
            Oman is that the more you add to the service, the more your services 
            come in demand. The government alone cannot satisfy the growing 
            needs of all sectors. For this reason, we privatised some projects 
            that are attractive to local and foreign investors and retained 
            still others that might not have been too appealing to private 
            players at this stage. The private sector can provide considerable 
            human resources as it offers many incentives and remunerations to 
            local and foreign recruits.<br>
            <br>
            <b>Regulatory landscape</b><br>
            The TRA is currently the regulator—or ‘‘watchdog’’—for the 
            Sultanate’s telecommunications industry. Broadcast transmission is 
            also part of the TRA’s remit. The TRA is a government organisation. 
            The country, through the TRA, initiated the process of award of a 
            second public mobile licence with the objective of introducing 
            competition, which will enhance the sector and benefit the 
            end-customer through competitive and reasonable prices, better 
            services and expanded choice. <br>
            <br>
            The policy framework in place for the country’s telecom sector also 
            aims at promoting infrastructure development that will increase tele-density 
            in the Sultanate, which presents future opportunities for new 
            operators in the country. Oman’s economic direction is in line with 
            the contemporary trends of trade and investment liberalisation, 
            which are adopted by the WTO, of which the Sultanate is a 
            full-fledged member.<br>
            <br>
            <b>Liberalisation &amp; privatisation</b><br>
            Last year, the government opened up the market, moving away from the 
            dominance of the newly formed Oman Mobile to allow a second mobile 
            operator, Nawras Telecom, which is expected to begin operations this 
            year, pending the issuance of the licence, which should be granted 
            soon. The result is that the Sultanate now has a more open and 
            competitive telecom market, ahead of some of the other countries in 
            the GCC, which are still in the process of unveiling a 
            liberalisation and privatisation roadmap. Moreover, proliferation of 
            the mobile services will strengthen support services including 
            mobile repairing. Needless to say, the new operator will create new 
            job opportunities for locals.<br>
            <br>
            A further substantial step towards privatisation is expected to take 
            place in the first quarter of 2005, when Omantel gets part-listed on 
            the Muscat Securities Market (MSM). The government has already 
            enlisted the financial advisory services of a consortium between 
            HSBC, BankMuscat and Fincorp, managed by the Ministry of Finance. 
            The consortium is evaluating Omantel’s business from both strategic 
            and financial perspective, working on the share value, evaluating 
            the company, and looking at the existing and future business 
            prospects. The Ministry of Finance plans to offer a part of the 
            government-owned Omantel in the form of an IPO, which will 
            definitely add to the activity of the MSM. <br>
            <br>
            The Omani economy stands to benefit from the opening up of the 
            telecommunications sector. Besides the significant benefits that 
            will be derived from competition in the sector, the process will 
            also guarantee another source of income to the treasury, thus 
            helping bridging the shortfall in financing, if any.<br>
            <br>
            <b>Fixed network</b><br>
            The government’s efforts to privatise the country’s telecom services 
            led to the legal and organisational establishment of Omantel in 
            mid-1999, which replaced the previous State-owned monopoly General 
            Telecommunications Organisation. While the fixed line market is 
            still a monopoly held by the State-owned operator Omantel, the 
            government has plans of liberalising the market in the future. <br>
            <br>
            In the government’s bid to speed up privatisation, the current 
            operator has been split into two separate companies under the 
            control of a holding company (Omantel). The separate mobile entity 
            (Oman Mobile) was created in February 2004. Omantel is further 
            expected to split, giving rise to an independent Internet services 
            company, much on the lines of Oman Mobile. Competition between the 
            fixed telecom suppliers is expected to improve the country’s fixed 
            line network and penetration figures. <br>
            <br>
            <b>Mobile communications</b><br>
            The Omani mobile market is growing fast. As of October 2004, there 
            were over 765,000 subscribers, an increase of over 29 per cent on 
            the previous year. This number is expected to explode with Nawras 
            Telecom introducing services early this year. With an expected 30 
            per cent growth in 2004, the number of mobile customers is 
            forecasted to reach 1.5 million by 2009, which would represent a 
            penetration rate of 51.7 per cent.<br>
            <br>
            Oman now has two mobile players – the incumbent Oman Mobile and the 
            Nawras Telecom, which is expected to launch operations soon. Nawras, 
            the winner of the second GSM licence, enjoys a 3-year exclusivity 
            period where no further mobile licences will be granted. The new 
            player has a 15-year deal with the government. <br>
            <br>
            Oman Mobile runs a GSM network and 2.5G technology is now available 
            although 3G technology too has been exhibited. The company 
            introduced a pre-paid service named Hayyak, and it now accounts for 
            62 per cent of its mobile customers. The government is also 
            encouraging the use of Wi-Fi. <br>
            <br>
            <b>The promise of the Internet</b><br>
            The government recognises that the Internet has enormous potential 
            for education, entertainment and business and wants everybody to be 
            able to have access to it and to use it confidently. Consumers and 
            business in the Sultanate benefit from low Internet access prices.
            <br>
            <br>
            Allowing and providing e-government service is an integral component 
            of the Sultanate’s IT Strategy. Oman’s plan to strengthen its 
            presence in the big world of e-government will get off to a major 
            start with the move to launch government convergence network by the 
            Ministry of National Economy this year. E-government is conceived as 
            a means of improving services to businesses and citizens, promoting 
            economic and social development, and improving the effectiveness and 
            efficiency of internal government operations.</font></p>
