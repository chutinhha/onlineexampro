            Executive President, Capital Market Authority</font></b></p>
            <p align="justify"><font face="Verdana" size="1">The Muscat 
            Securities Market (MSM) was established in 1988. Its development 
            landscape has bright as well as anaemic spots. The number of listed 
            companies increased from 64 in 1993 to 144 in November 2004; market 
            capitalisation as percentage of GDP increased from 18.8 per cent in 
            1993 to 43.4 per cent in November 2004; and share price index rose 
            from 113 in 1993 to 3370 (337 on old scale) at the end of November 
            2004. The journey through time, of some of the indicators, is in the 
            table on the right.<br>
            <br>
            The progress has, however, not been uniform over the period. The 
            lessons from the journey of last 16 years have made us cautiously 
            optimistic. A decade of experience (1988-98) revealed that the 
            operational activities of the MSM over-shadowed the regulatory 
            needs. The boom followed by the precipitous slump of 1998 made us 
            focus on regulatory issues. This led to restructuring of securities 
            market, providing for separate legal entities for trading (MSM), 
            depository functions (MDSRC) and regulation (CMA).<br>
            <br>
            The reforms of the last few years have enhanced the integrity, 
            transparency and efficiency of the operations of the securities 
            market. Integrated screen-based trading and settlement, with all its 
            attendant benefits, is firmly in place. CMA, as regulator, serves 
            the role of impartial watchdog. Establishment of depository has made 
            the settlement in securities fast and error free. Rolling settlement 
            on T+3 basis is at par with currently applicable international 
            standards. With these reforms already in place, we are on the right 
            track of reforms. The reform process is still ongoing in the areas 
            of settlement to minimise systemic risks, management of large 
            exposures, institutionalisation of SRO concept, regulatory structure 
            of collective investment schemes and certification requirement for 
            intermediaries etc.<br>
            <br>
            Some of the challenges facing the Omani securities are increasing 
            the capitalisation of listed companies, need of reduction in per 
            unit cost associated with securities market operation and regulation 
            (caused by smallness of market), enlarging the range of products, 
            diversification away from finance and banking sector, encouraging 
            institutional interest in MSM, increasing the capitalisation levels 
            of intermediaries, improving human resources skills, 
            institutionalising self-regulation, risk minimisation in trade 
            settlement system, establishing securities lending and borrowing 
            mechanism, encouraging market makers or liquidity providers taking 
            into account systemic inadequacies (not very highly capitalised 
            intermediaries, absence of short-selling, securities lending 
            mechanism still under consideration and non-utilisation of repo 
            market by the intermediaries etc.), enforcement challenges towards 
            curbing insider trading, sharpening the jurisdictional clarity in 
            regulatory framework (e.g., between CBO and CMA), improvement in 
            profitability of real sector, dispersing the ownership of companies 
            away from business families and institutions, and enhancing public 
            awareness about role of capital market as avenue of investment. <br>
            <br>
            Vision 2020 for the Sultanate of Oman envisages doubling of per 
            capita income by the year 2020. A conservative estimate shows that 
            in order to achieve this growth, MSM alone would require to raise 
            RO870 million in the 20th year, which will keep increasing further 
            in accordance with growth targets. MSM is equipping itself to meet 
            the planned target.<br>
            We lay great emphasis on proper governance of corporates as a tool 
            towards strengthening investor confidence and ensuring the stability 
            and integrity of financial markets. We were probably the first in 
            the GCC region to frame a code for corporate governance in 2002. <br>
            <br>
            The salient features of our corporate governance framework are 
            taking measures to empower minority shareholders, promoting the 
            concept of shareholder watchdog under the banner of an independent 
            agency, and leveraging the concept of informed ownership, enhancing 
            the quality of audit through empowerment of audit committee, stress 
            on forward-looking disclosures in the annual report in the form of 
            “Management discussion and analysis” report, encouraging 
            institutional investors to oversee corporate governance practices in 
            investee companies, casting of responsibility on directors towards 
            internal control and risk management, and disclosure about the 
            processes adopted to achieve this objective and disclosure 
            requirements especially regarding conflicts of interest and related 
            party transactions.<br>
            <br>
            Some of the legal measures taken in recent past include transfer the 
            affairs of listed companies wholly to CMA (save incorporation and 
            liquidation) providing a single window to the entire affairs of the 
            listed companies, mandating the appointment of compliance officer 
            for listed companies, capital adequacy norm for intermediaries to 
            mitigate exposure risks, comprehensive guidelines to curb possible 
            money laundering activities, new listing directive requiring the 
            redistribution of the listed companies with regard to the 
            performance and the capitalisation level, establishment of Investors 
            Trust Fund, rules for trading of rights in shares in MSM, disclosure 
            of dividend policies of listed companies and rules for private 
            placement of shares.<br>
            <br>
            Foreign participation in MSM listed companies increased from 10.65 
            per cent of capital by the end of 1995 to 17.25 per cent of capital 
            by the end of September 2004. Some of the direct incentive to 
            attract foreign investment in Omani listed companies are 
            restriction-less repatriation of capital or profit, free 
            convertibility of the Rail Omani at fixed exchange rate with the 
            dollar, low corporate profit tax of 12 per cent over RO30,000 and no 
            tax on dividend or capital gain. <br>
            <br>
            On regulatory front, our welcome features which attract foreign 
            investment are existence of a robust regulatory structure, clear 
            rules and regulations in most of the areas, not-so-poorly 
            capitalised brokerage companies, availability of trading instruments 
            other than shares like bonds and mutual fund units, existence of 
            settlement fund to ensure timely settlement even in the event of 
            default by a broker, establishment of brokers organisation which 
            would evolve into a responsible self-regulatory organisation (SRO) 
            with the passage of time, a sound code of corporate governance and 
            very effective anti-money laundering measures.<br>
            <br>
            We have prepared a strategic plan for the securities industry in 
            Oman which contains 107 recommendations. Some of the important ones 
            are:<br>
            <br>
            <b>Invigorating the market structure:</b><br>
            • Dialogue with the appropriate authorities to frame incentive 
            structure for merger<br>
            • Making transparent code of take-over<br>
            • Efforts towards unification of GCC securities market<br>
            • Privatisation of MSM and Muscat depository<br>
            • Strategic alliance with other GCC markets<br>
            • Bringing the concept of GCC depository receipt<br>
            • Reconstruction on MSM index with a view to impart trading 
            attributes.<br>
            <br>
            <b>Liquidity enhancement measures:</b><br>
            • Exploring the mechanism of market making in illiquid scrips<br>
            • Re-examining margin trading with adequate safeguards<br>
            • Examining the prospects of allowing short-selling<br>
            • Making regulations for negotiated trades<br>
            • Possibility of remote terminal trading and Internet trading<br>
            • Lending and borrowing of securities.<br>
            <br>
            <b>Diversification of trading instruments:</b><br>
            • Making transparent bonds regulations<br>
            • Bringing rating agencies in Oman and allowing them to take away 
            some load from regulatory authorities<br>
            • Closed-ended real estate funds<br>
            • Trading of commercial papers and certificate of deposits<br>
            • Exploring possibilities of introducing derivatives on index.<br>
            <br>
            <b>Institutional investment: </b><br>
            • Introducing Exchange Traded Funds<br>
            • Making regulatory framework for venture funds<br>
            • Introducing Bond Funds and Real Estate Funds<br>
            • Entering into dialogue with the government to restructure 
            government investment in MSM with particular focus towards 
            establishment of venture capital fund and market making fund<br>
            • Steps towards setting up mechanism to gather data, calculate and 
            disseminate Oman inter bank offered rates (OIBOR) and Oman inter 
            bank bid rates (OIBID)<br>
            • Entering into dialogue with government to review guidelines 
            regarding investment of public pension funds in MSM and making 
            guidelines on investment discipline, use of leverage, performance 
            presentation standard and other issues.<br>
            <br>
            <b>Corporate governance measures:</b><br>
            • Taking further measures to empower minority shareholders<br>
            • Devising mechanism to enhance the quality of audit of the listed 
            companies<br>
            • Prescribing disclosure about wealth creation on new value metrics 
            like EVA/ MVA/CFROI etc.<br>
            • Encouraging institutional investors to oversee corporate 
            governance practices in investee companies.</font></p>
            <p align="justify"><font face="Verdana" size="1">In efforts to 
            encourage more IPOs, we strive to create facilitative regulatory 
            environment. Public offering of Omantel is already in advanced 
            stages. The government has recently divested its holdings in Oman 
            Cement, Oman Flour Mills and Al-Maha Petroleum. All of these were 
            cheered by the MSM investors. We foresee the trend to continue with 
            dramatic impact on liquidity landscape of MSM, which would be a 
            catalytic force to attract foreign investors. <br>
            <br>
            We also foresee that some of the well-established profitable family 
            companies would go public as there is no option other than raising 
            money from public if one has to participate in the growth climate of 
            the economy. Our government also encourages companies to go public 
            through incentives like award of big tenders only to public 
            companies or to those who convert into a public company. <br>
            <br>
            On the secondary market front, MSM plans to revamp the existing 
            trading and settlement system to increase the speed of trading at 
            peak hours, examine the feasibility of providing trading system in 
            dispersed locations and even beyond Muscat, Internet based trading 
            and making the depository database more investor friendly so that 
            investors can confirm their balances through the Net.<br>
            <br>
            With the above measures in place, we plan to make MSM a market which 
            is complete in terms of richness of instruments, 
            efficient––procedurally, allocationally and informationally––and 
            with minimum of systemic risks.</font></p>
