            strengthen and innovate and enhance efficiency both product wise as 
            well as technology wise. Banks must be prepared to face greater 
            competition and remain vigilant to the developments and changes that 
            take place in the financial and banking arena globally, due to 
            increased exposure to risks and contagion. <br>
