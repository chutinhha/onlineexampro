            <br>
            <b>The oil factor</b><br>
            With high oil prices, both balance of payments and fiscal positions 
            will remain comfortable and the higher growth in investment 
            expenditure in the budget bodes well for growth. The government’s 
            long-term priority is to diversify away from the economy’s reliance 
            on relatively limited oil resources. Towards this end, the objective 
            remains to reduce the fiscal dependence on hydrocarbon revenues, 
            undertake structural reforms to sustain the growth momentum of the 
            non-oil sector and ensure monetary and financial stability in the 
            economy.<br>
            <br>
            Oman’s banking and financial sector has evolved over time acquiring 
            a prominent role in promoting the development and growth process of 
            the economy. In a bank-dominated financial system, effective 
            intermediation has enabled the mobilisation of savings and their use 
            for investment purpose. In view of the oil dependent nature of the 
            economy and the important role of public expenditure in sustaining 
            the overall growth process and the increase of private investment, 
            effective intermediation of funds through a stable banking system 
            holds the key to ensure efficient interplay of aggregate demand and 
            supply in the system.<br>
            <br>
            Banks have a critical role to play in furthering the process of 
            privatisation and diversification, since all private initiatives to 
            attain this objective need to be complemented by adequate financing. 
            Given the well-established relationship between finance and growth, 
            and in view of the dominance of banks in the financial system of 
            Oman, banks play a notable role in driving the Omani economy.<br>
            <br>
            <b>Consolidation time</b><br>
            The banking sector in Oman continues to witness restructuring and 
            consolidation mainly through mergers, acquisitions and other 
            arrangements which is the outcome of a natural response to the 
            forces of change and technological advancements. Adequate capital is 
            one of the important indicators of the financial strength of banks. 
            Mirroring improvements in the health of the financial system, the 
            overall capital adequacy ratio of the commercial banks in Oman stand 
            close to 18 per cent, significantly higher than the minimum required 
            level of 12 per cent.<br>
            <br>
            As part of heightened supervisory oversight of the risk management 
            systems in banks, the on-site examinations of the Central Bank of 
            Oman have been made risk-focused and banks have been reporting their 
            risk profiles at monthly intervals. This approach helps in detecting 
            simmering symptoms of instability, both at the institutional and 
            systemic levels. Other initiatives that have been undertaken to 
            foster financial stability include the introduction of a Prompt 
            Corrective Action framework, a policy on securitisation of assets 
            and an upgraded Bank Credit and Statistical Bureau system. <br>
            <br>
            The CBO has taken the lead along with other financial institutions 
            in Oman to pursue policies that encourage Internet banking, 
            electronic fund transfers, and is in the final stages of introducing 
            Real Time Gross Settlement Systems (RTGS). Electronic means of 
            delivering services by banks will continue to develop at a rapid 
            pace and the CBO would encourage banks to deploy the latest 
            technology for customer satisfaction.<br>
            <br>
            <b>Future challenges</b><br>
