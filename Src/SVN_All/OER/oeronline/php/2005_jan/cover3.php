            Weathering the storm<br>
            <br>
            </font><font face="Verdana" size="1"><b>Along with its airline 
            operations, Oman Air will continue to strengthen its airport 
            services business at Seeb and Salalah airports, confirms:<br>
            <br>
            <img border="0" src="im4.jpg" align="left" hspace="6" width="150" height="173">Abdul 
            Rahman Al Busaidy<br>
            Chief Executive Officer, Oman Air</b><br>
            <br>
            After surviving SARS, the war in Iraq, and the economic slowdown in 
            2003, the airline industry was set to return to profitability in 
            2004. However, the steep increase in oil price has added almost $12 
            billion to the industry’s costs and, as a result, the industry is 
            expected to end the year with the losses in excess of $4 billion.<br>
            <br>
            However, on a positive side, the international passenger traffic is 
            growing at a remarkable rate. The year-to-date traffic has grown by 
            17 per cent in the first 10 months of 2004. The growth has surpassed 
            expectations, and can be attributed primarily to three factors: 
            improvement of the underlying traffic trends, a strong recovery from 
            the SARS crisis, and the impact of cyclical economic growth factors. 
            Average seat factor during the same period was a healthy 74.6 per 
            cent. The growth in passenger traffic is expected to continue in 
            2005, albeit at a slower pace. <br>
            <br>
            The growth in passenger traffic has varied across the world, from a 
            low of 11 per cent in Africa and Europe to a high of 24 and 27 per 
            cent in Asia Pacific and the Middle East, respectively. While 
            passenger traffic growth has been highly encouraging, the yields 
            continue to suffer from intense competition as each airline tries to 
            protect and enhance its market share of growing passenger market. 
            The entry of low cost carriers and their success in the US, Europe 
            and Far East has also intensified the pressure on fares. Yields are 
            declining at an alarmingly high average rate of 2 to 3 per cent per 
            annum.<br>
            <br>
            With rising fuel prices, declining yields, and competition from low 
            cost carriers, cost reduction has been a priority for scheduled 
            network carriers. Various measures are adopted to reduce operating 
            and distribution costs. These measures include structural reforms, 
            simplification of business processes, new IT initiatives, route 
            network restructuring, fleet rationalisation, staff reduction etc.
            <br>
            <br>
            <b>The regional picture</b><br>
            Passenger traffic in the Middle East is currently 42 per cent above 
            2001. During the first 10 months of 2004, passenger and cargo 
            traffic in the region has grown at a faster pace of 27 per cent 
            each, compared to the global average of 17 and 14 per cent, 
            respectively. The growth during this period is more or less matched 
            by the increase of 24 per cent in capacity. The region has recovered 
            from the adverse effects of war and political instability. The 
            growth in passenger traffic is fuelled by increasing transit traffic 
            and expanding regional tourism.<br>
            <br>
            Sustained higher-than-expected oil prices over the last two years 
            have led the regional economies to invest heavily in non-oil 
            sectors, including tourism, free trade zones, industrial activities, 
            agriculture and infrastructural development. This has given a boost 
            to the economic growth which in turn has increased passenger traffic 
            in and through the region. Countries such as the UAE and Qatar have 
            focused on developing their aviation sector to propel their 
            economies and develop major business, tourism and transit hubs. 
            These countries continue to invest heavily in their national 
            airlines and airports, with an objective to provide a world-class 
            carrier and facilities.<br>
            <br>
            Emirates Airline continues to expand and will have 81 destinations 
            in its network by the end of 2005. Their current fleet of 64 
            aircraft will expand to 169, with 45 jumbo A380s, by 2012. Qatar 
            Airways will expand its current fleet from 36 to 52 aircraft in 
            2008. It added 12 aircraft in 2004 and expanded its route network to 
            59 destinations. Air Arabia, in a short span of one year, operates 
            to 14 destinations with five aircraft. Etihad, which has also opened 
            14 destinations in the very first year of existence, has announced 
            orders for 29 wide body aircraft. <br>
            <br>
            It is evident that airlines are adding huge capacity in the region 
            in anticipation of continued upturn in passenger traffic. However, 
            the 27 per cent growth in passenger traffic witnessed in 2004 may 
            not be sustainable in the future, and such steep and continued surge 
            in capacity may outpace the demand in coming years. Competitive 
            pressures are already seen in the form of low fares offered by some 
            of these airlines. The commercial viability of such fast paced 
            massive expansion plans is questioned by many industry experts.<br>
            <br>
            The Indian sub-continent, which has offered high density and high 
            yield routes over many years, has been a source of high profits to 
            the airlines in the region. However, this may be about to change 
            with the liberal aviation policy India is expected to follow in the 
            coming years. Recently, when India announced an open sky policy for 
            a limited period until March 2005, airlines were quick to deploy 
            additional capacity on Indian routes, and in the process, flooded 
            these routes with excess capacity. For example, Qatar Airways 
            increased their operations from 19 flights to 51 flights per week. 
            Furthermore, Air India is planning to order 50 new aircraft and is 
            about to launch a low cost airline under the name Air India Express 
            which will acquire 18 aircraft.<br>
            <br>
            In summary, the regional aviation scene is seeing and benefiting 
            from high passenger demand, development of excellent infrastructure 
            and regional economic boom. However, on the other hand, the growth 
            in capacity has been quite significant, and may pose concern to the 
            commercial viability of certain airlines in the event they are 
            unable to deploy such capacity profitably.<br>
            <br>
            <b>Oman Air strategy in 2005</b><br>
            The government of Oman has adopted a policy of having two national 
            airlines: Oman Air and Gulf Air, and has expressed its desire to see 
            the two airlines cooperate with each other. With this mandate, Oman 
            Air has chosen to focus on developing itself as a niche regional 
            carrier with Muscat as its hub. With this objective, Oman Air will 
            maintain and strengthen its presence in the Middle East, Indian 
            sub-continent and East Africa. <br>
            <br>
            Oman Air is a unique airline in the region in the sense that it is 
            the only airline that is pre-dominantly held by private 
            shareholders. Its shares are listed and traded in the local stock 
            market. Oman Air thus operates as a commercial enterprise with an 
            objective to add economic value for its shareholders, keeping in 
            mind the national objectives of improving tourism and providing air 
            transportation to the residents of the Sultanate. <br>
            <br>
            Despite a small route network, Oman Air has successfully established 
            and strengthened its presence on certain routes. With the 
            acquisition of the sixth B 737 aircraft in March 2005, Oman Air will 
            operate all its international services with jet aircraft. Efforts 
            are underway to lease its turbo prop ATR 42 fleet which will be 
            rendered surplus. This will add a new stream of revenue and also 
            recover ownership cost of these aircraft.<br>
            <br>
            Oman Air is conscious of the immense competitive pressure brought 
            upon by the huge capacity mounted by competing airlines. The airline 
            plans to combat these pressures through differentiating its product 
            and optimising its top line revenues through various yield 
            improvement measures. <br>
            <br>
            At the same time, Oman Air will pursue various IT initiatives, such 
            as e-ticketing, Internet booking, revenue optimisation, flight 
            planning and scheduling, ERP solution etc, to optimise its processes 
            and cost structure. <br>
            <br>
            Along with its airline operations, Oman Air will continue to 
            strengthen its airport services business at Seeb and Salalah 
            airports. The company will invest in various IT solutions, 
            equipment, and manpower training to ensure it is able to offer the 
            highest standards of service to all airlines including Oman Air.</font></p>
