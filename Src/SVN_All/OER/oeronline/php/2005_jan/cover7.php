            </font><font face="Verdana" size="1">In the 34-year period since His 
            Majesty Sultan Qaboos Bin Said came to the throne, Oman’s education 
            system has undergone a complete transformation. In 1970, there were 
            only three schools in the country, providing education for 900 male 
            primary level students. By 2004, this had grown to 1,045 government 
            schools, covering all levels up to grade 12, with a further 155 
            private schools under the supervision of the Ministry of Education. 
            The school student population now totals over 600,000, 48 per cent 
            of whom are female. This quantitative expansion of educational 
            provision has enabled the country to achieve the important goal of 
            providing universal access to education.<br>
            <br>
            The ministry has now shifted its focus towards achieving the equally 
            important goal of qualitatively improving its education system. As 
            recognised by the Vision Conference “Oman 2020”, the needs of the 
            emerging global and national economies are for employees who are 
            creative, flexible, technologically literate, can think analytically 
            and are skilled communicators. The ministry’s duty, therefore, must 
            be to provide an education system which allows students to acquire 
            the skills they need for life in a globalised world and, thereby, 
            adequately prepare them for the requirements of higher education and 
            the labour market.<br>
            <br>
            <b>The reforming process</b><br>
            To achieve this aim, the ministry has developed a comprehensive plan 
            to introduce a new system which adopts aspects of best educational 
            learning theory and practice while remaining firmly rooted in 
            Islamic principles and Omani cultural identity. This plan involves 
            reforming all aspects of the education system and at all grades.<br>
            <br>
            A curriculum review was carried out to ensure that subjects offered 
            to students were relevant to their present and future needs. Certain 
            key subjects, such as science, mathematics and English language have 
            been strengthened and new subjects such as life skills and 
            information technology have been introduced.<br>
            <br>
            Teaching and learning strategies to encourage transferable life-long 
            skills such as autonomous and cooperative learning, communication, 
            critical thinking, problem-solving and research techniques have been 
            developed. The aim is to create an environment that allows students 
            to learn how to learn and encourages them to take responsibility for 
            their own learning. <br>
            <br>
            A wide range of assessment instruments, including teacher classroom 
            assessment, has been introduced. As a result, student performance on 
            a range of understanding and skills, such as measurement through the 
            handling of real equipment in science and speaking in Arabic and 
            English languages, now plays an important contribution to students’ 
            overall marks at all grade levels. <br>
            <br>
            <b>The introduction of student choice</b><br>
            In another move to make the system more relevant, the “Science” and 
            “Arts” streams in grades 11 and 12 have been replaced by a core and 
            elective programme. This will allow students to determine individual 
            curricula appropriate to their needs and interests and to specialise 
            further in the field of their choice. <br>
            <br>
            For example, a student who has a particular interest in mathematics 
            and science will be able to choose pure mathematics and a science 
            subject in the core and to study one or two other science subjects 
            in the electives. On the other hand, a student whose interests lie 
            in the arts side might choose applied mathematics in the core, which 
            contains units of study related to economics and business in 
            addition to other mathematics topics of practical use and 
            application, and to choose humanities courses in the electives.<br>
            <br>
            This core and elective programme will make learning more relevant to 
            the needs of students and society in general. Following the end of 
            grade 12, students will want to head off in a variety of directions. 
            The new system allows students to pursue studies relevant to their 
            future aspirations.<br>
            <br>
            <b>Information technology</b><br>
            Internationally, it is now widely accepted that information 
            technology has a critical role to play in helping to prepare 
            students for the rapidly changing circumstances of the 21st century. 
            When students leave school, either to continue their studies or into 
            the world of work, they will be expected to be literate in 
            information technology. <br>
            <br>
            Traditional book-based libraries have been replaced by learning 
            resource centres in all Basic Education schools. These centres are 
            equipped with 15 computers and a range of audio visual aids and 
            print materials. In the Basic Education second cycle schools (i.e., 
            grade 5 upwards), a separate classroom containing 20 additional 
            computers has also been provided. In September 2004, all schools 
            teaching grade 11 students were also supplied with a suite of 
            computers.<br>
            <br>
            The approach adopted by the ministry is to teach students the 
            principles of information technology as a separate subject but also 
            to integrate the use of computers into topics studied in other 
            subject areas. <br>
            <br>
            A wireless network has been introduced into 224 schools, thus 
            providing teachers and students with mobile high-speed access to the 
            Internet and e-learning applications in their subject classrooms. An 
            educational portal system, which will enable schools in all regions 
            to be connected to the Internet, is due to be introduced in 2005.<br>
            <br>
            In providing students with the required IT skills, the ministry is 
            giving its students the tools they need to make the most of their 
            opportunities in later life. In helping to create a population which 
            is literate in IT, the ministry is also making a significant 
            contribution to the future economic and social well-being of the 
            country as a whole.<br>
            <br>
            <b>Linking with the private sector</b><br>
            Preparations are underway at the ministry for future projects, some 
            of which envisage the active participation of the private sector. 
            For example, there are plans to establish a career guidance service 
            and the involvement of local businesses might provide them with 
            valuable recruitment advantages. Moreover, in return for the 
            ministry promoting a business’ name and/or its products or services, 
            private companies might be persuaded to put something tangible back 
            into education through a sponsorship arrangement.<br>
            <br>
            It is very important for the Ministry of Education and the Omani 
            business community to work cooperatively and to develop 
            partnerships. After all, the goal of the ministry’s reforms is to 
            establish a world-class education system that will prepare students 
            for a productive life in Oman’s modern knowledge-based economy. 
            Supporting reforms that are investing in the future growth and 
            development of Omani society can only be in everyone’s best 
            interests.</font></p>
