<br>
<b>Matt Harrison elucidates how marketing, corporate communications, PR and 
sales folk should and shouldn’t deliver presentations</b><br>
<br>
<img border="0" src="im20.jpg" align="left" hspace="6" width="150" height="180">How 
do you find the prospect of being asked to speak in public? Are you a brilliant 
public speaker who delivers their script-less presentation effortlessly, and 
peppers their speech with witticisms before finishing off to a round of 
applause? Or does the whole idea of standing in front of a group fill you with 
absolute terror? In fact, recent research suggests that 55 pert cent of people 
are more afraid of public speaking than death. <br>
If you’re in the marketing, sales, corporate communications or public relations 
game – being asked to prepare and deliver presentations comes with the turf. 
Indeed, we’ve all sat through corporate presentations that made no sense, were 
overly complicated, humourless or simply sent us to sleep. So what can you do to 
help a presentation go smoothly and have the right impact?<br>
<br>
<b>Real content</b><br>
The content of a presentation is always extremely important, but an interesting 
topic can still turn into a poor presentation if not delivered correctly. Use 
pictures or graphs – a picture says a thousand words, right? Slides are used to 
build your point, so don’t feel you have to put everything on them. Just use 
them as a prompt for you to talk around. <br>
<br>
Market studies show that effectiveness of communication is increased 40 to 50 
per cent when a visual is added to the spoken word. Using mostly visuals or all 
visuals and no words shows extreme confidence, even when presenting in front of 
the least friendly of audiences. And using all visuals and no text just makes 
presentations much more fun.<br>
<br>
Ideally, structure your presentation around three key points. If you give people 
too much information, they’ll forget most of it or just lose interest. The 
presentation should have a beginning, middle and an end. In short, structure it 
in a way that it takes the audience on a logical journey. <br>
<br>
However, to make a really effective presentation, you’ll need to do more than 
just talk. You’re responsible for providing the audience with a presentation 
that’s lively, informative and delivered with enough enthusiasm to maintain 
their interest.<br>
<br>
<b>What did he say?</b><br>
Most people remember only two or three things from any presentation after two 
days. The rest is fluff. To make yours truly memorable, you must take advantage 
of how people absorb information. When preparing a presentation, it’s worth 
remembering the acronym VAK, which stands for:<br>
• Visual - seeing it<br>
• Auditory - hearing it<br>
• Kinaesthetic - feeling or touching it<br>
<br>
Everyone tends to use one of these senses more than the other two. So make sure 
you use a combination of all three senses to gain maximum impact.<br>
<br>
<b>How much time do I have?</b><br>
You will usually be given a set amount of time in which to deliver your 
presentation. When you plan the content, practice reading your notes to check 
that you can deliver it in the time allowed (this practice will also help you 
remember the content of your presentation and give you confidence in delivering 
it). If you are going to decide on the length of your presentation, bear in mind 
that the attention of an audience can diminish after as little as 10 minutes.<br>
<br>
<b>Who’s the audience?</b><br>
When you devise your presentation, take into account the background and needs of 
your audience. Ask yourself what they probably already know about your topic and 
what information they need to know. When you decide upon the order in which you 
will deliver your information, ask whether this will appear logical to the 
audience.<br>
<br>
<b>Looks good</b><br>
No matter what content your presentation contains, it’s important you follow 
some simple design principles. The font you use should be clear and large enough 
to be legible from the back of the room, colours should be bright – but not so 
bright that they give the audience a migraine. If any element of any of your 
slides elicits the comment ‘what’s that there for?’ delete it.<br>
<br>
<b>PowerPoint overkill</b><br>
Many presenters become over-reliant on PowerPoint. At the end of the day, 
PowerPoint is just a tool – and a blunt one at that. The best presentations come 
alive when the presenter weaves in compelling stories and examples of how 
his/her product or service brings benefits to the prospect’s business. <br>
<br>
Packing slides with far too much information has got to be the most consistent 
mistake people make and, as a rule of thumb, use no more than 40 words per 
slide. The more prepared you are, the more confident you’ll appear, but don’t 
fall into the trap of scripting your entire presentation. A speech that reads 
well will sound horrible. Write for the ear, not the eye. <br>
<br>
Most presenters love bullet-point text. Unfortunately, it isn’t particularly 
effective. Why? Processing text is a left-brain activity. When we read, we 
filter and eliminate based on existing perceptions; whatever information our 
brains don’t filter out goes into short-term memory. In contrast, sensory-based 
information is much more likely to go into long-term memory. Think of ways to 
replace bullet points with something more visual – for example, an animated 
flowchart (with supporting images) that walks the audience through a series of 
steps and is triggered by mouse clicks. <br>
<br>
Enthusiasm for your subject is essential, not least because it means you can 
still carry on if your laptop packs up. Bring an element of joy to your speaking 
opportunities. Behaving too serious can make you look and sound utterly boring.<br>
<br>
<b>The venue</b><br>
Consider the venue. If you’re going to be using an LCD projector, will the room 
be dark enough? Is there a screen available? Think about the size of the room – 
will your audience be able to hear you? Always use a microphone—ideally, a 
collar microphone—if you’re speaking to more than 15 people, but never shout 
when you’re using one! Larger rooms may also require changes to the way you 
deliver your presentation. The bigger the room you speak in, the slower you 
should speak. Remember, it takes time for the sound to bounce around the room.<br>
<br>
<b>Pointers</b><br>
What follows is a series of tips on how you should and shouldn’t present. If you 
want to make the right impression, take heed. This is how not to do it.<br>
<br>
<b>Write a script: </b>It’s much easier to read from a script. You don’t have to 
worry about forgetting your lines and your audience will hear all your ideas.<br>
<br>
<b>Dive in:</b> Nobody is interested in listening to a lengthy introduction; so, 
dive straight in and tell them all about it.<br>
<br>
<b>Use multiple &amp; complex bullet points: </b>Get as much as you can onto the 
slide. No need for graphics, folk never understand them anyway. <br>
<br>
<b>Use jargon and clever words:</b> Your audience are experts in their field. 
Don't patronise them with plain language. Use as much industry-slang as 
possible.<br>
<br>
<b>No narrative:</b> Your audience is intelligent; so, there’s no need to lead 
them through your argument. Let them work it out for themselves. <br>
<br>
<b>Make a few spelling mistakes:</b> Just in case anyone nods off, you can’t 
beat a good spelling mistake to get your audience’s attention back on track.<br>
<br>
<b>Use complicated multimedia: </b>There's nothing like a bullet point flying 
across the screen accompanied by a screeching noise. <br>
<br>
<b>No summary required:</b> You’ve told them everything; so, they’ve obviously 
understood it all. Don’t bother to re-cap, it’s a waste of your time and theirs.
<br>
<br>
<b>Let’s now have a look at how it should be done.</b><br>
<b>Script-free:</b> If you read from a script, people get bored. Your audience 
wants to hear you, not a prepared statement. Don’t rush. Include deliberate 
pauses to give time for the audience to think or reflect on your points.<br>
<br>
<b>Use an agenda: </b>It’s the oldest presentation maxim: ‘tell them what you’re 
going to tell them...etc.’ but it’s so useful and ignored so frequently.<br>
<br>
<b>Know your onions:</b> Folk that just read the bullet points don’t know their 
subject. <br>
<br>
<b>Tell a story:</b> Stories are what people want to hear. If your presentation 
has a beginning, middle and an end, they’ll be happier even if the message 
you’re tasked with getting over is not the world’s most exciting.<br>
<br>
<b>Think Minimal:</b> One single idea is the most you can expect an audience to 
come away from your presentation with. So don’t put up lists and lists of ideas. 
If you have to present a variety of different concepts, try and relate them back 
to your overall message.<br>
<br>
<b>Clean copy:</b> Proof reading is as necessary in a presentation as it is in 
any other written communication.<br>
<br>
<b>The clever stuff:</b> The best presentation is where someone paces the stage, 
oozing enthusiasm, telling a great story and backing it up with the odd 
crude-but-effective slide. Forget the presentation gimmickry.<br>
<br>
<b>Strong Finish:</b> If you’ve followed my advice and fail to wrap everything 
up, then your time has been wasted. The end is your last chance to make a real 
impact. So, hit the audience with a concise conclusion. Let’s be clear, great 
conclusions draw a line under the presentation and cement your message.</font></p>

