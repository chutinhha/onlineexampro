            <b>Briefly describe your management beliefs…</b><br>
            I am a very earnest and straightforward person, and I have always 
            been honest in my dealings – whether in the UK or here in Oman. I 
            think part of the reason for success in any business is to be honest 
            and to promise only what you can achieve, and then deliver what you 
            promised. <br>
            <br>
            My management style is an interactive one. My first task when I join 
            a company afresh is to meet everybody – right from the cleaners up 
            to the highest management level in my division. I think an 
            organisation is like a pyramid – when you are at the top of the 
            pyramid or just about at the top, you must realise that you need a 
            strong base to hold the structure, and you need to understand how 
            this base is operating, and then you can understand the weaknesses 
            and strengths of the organisation. <br>
            <br>
            So, I am hands-on. However, I do like to delegate certain jobs to 
            the professionals. It has to be a combination of both approaches. In 
            such a large organisation, doing everything yourself is impossible – 
            however, you need to know what’s going on by taking regular updates 
            from the team and feedback from customers. I also follow an 
            open-doors policy, and any employee of the organisation can walk in 
            at any time and talk freely about any ideas that he or she might 
            have or even any issues that they would like to escalate. <br>
            <br>
            I am also a big believer in the power of training. The brand will 
            definitely have its impact, but I think the people who represent 
            that brand are equally important, and they should be aware and 
            capable of handling any kind of situation. Also, a manufacturer’s 
            brand is important, but it must be allied to a strong local dealer’s 
            brand. While the former guarantees product quality, the latter 
            offers the security of the buying experience.<br>
            <br>
            In a nutshell, my ethos is:<br>
            a. Improve basic efficiency all the time.<br>
            b. Benchmark your current performance against your past and 
            competition’s. <br>
            c. Serve your customers well or someone else will.<br>
            d. Make money. If you don’t, you can’t do anything else. <br>
            e. Provide the backbone – staff development. <br>
            <br>
            <b>What drives MHD?</b><br>
            An undivided focus on customer service and satisfaction, and a 
            strong thrust to employee motivation and development. The success of 
            MHD LLC is down to a mixture of staff training and care—what we call 
            the MHD way—which translates into excellent customer satisfaction 
            and a regional growth strategy that enables MHD management to take a 
            hands-on approach. MHD is proof that focusing on customer service 
            pays off – but you have to maintain a tight rein over each outlet to 
            ensure that all staff is motivated to operate at the highest levels 
            of efficiency. MHD also has a strong sense of community spirit, 
            recognising that this helps to establish a good reputation in the 
            local area.<br>
