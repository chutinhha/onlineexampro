            the business. I have been in Oman for about 18-20 months now, and I 
            can say that the Omani market is a developing market – it has a huge 
            potential. The year 2004 has been a great success for us. In 
            Automotive business, we have had a volume increase in both sales &amp; 
            after-sales, and our customer satisfaction measure is getting 
            increasingly better. <br>
