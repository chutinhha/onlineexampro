            <p align="justify"><font color="#FF0000" face="Verdana">
            <font size="4">An Epic of a car!</font></font><font face="Verdana" size="1"><br>
            <br>
            <b>What do you call a dependable vehicle with tasteful refinements 
            and upscale features? OER test-drives the new Chevrolet Epica 2.5</b><br>
            <br>
            You can pay a lot for luxury appointments. Or you can pay a little 
            for the same appointments. If you’d prefer to just pay a little, 
            then the Epica is your car. It is a comfortable, quiet, smooth and 
            easy car to drive. In fact, it drives and rides like a much more 
            expensive car. <br>
            <br>
            The Epica provides style and elegance both inside and out. The Epica 
            represents the very definition of a modern exterior with body-colour 
            bumpers, mirrors, door handles and side mouldings, fog lamps and 15&quot; 
            aluminium wheels. Inside, you’ll feel positively luxurious with 
            4-speed automatic transmission, power windows and locks, heated 
            outside rear view mirrors, reclining front bucket seats, 8-way power 
            driver seat adjuster, adjustable steering column, air conditioning 
            with filter and wood grain accents.<br>
            <br>
            The Epica’s cabin is very quiet at highway speeds and the ride is 
            comfortable with some lean in the corners – a four wheel independent 
            suspension is standard. <br>
            <br>
            The standard speed-sensitive power rack and pinion steering offered 
            low effort at slow speeds and a firmer feel at higher speeds. Four 
            disc brakes are standard, and ABS is optional on LS models, standard 
            on LT trim. Overall, I found the Epica a very pleasant, comfortable 
            car to drive. The Epica’s interior is very attractive and well 
            finished. The quality of the leather, plastics, chrome and wood trim 
            is excellent for this price class and the panel gaps and fit are 
            very good. Front and rear headroom and legroom are adequate for four 
            or five adults.<br>
            <br>
            <b>The exteriors</b><br>
            Upscale amenities like power eight-way driver’s seat, reclining 
            front bucket seats, power windows, power door locks and exterior 
            mirrors, remote keyless entry, air conditioning, CD player, and 
            simulated wood grain accents are all standard. This car is a great 
            value for money. In fact, it feels like a luxury car for the price 
            of a mid-size! There is little cabin noise, and it rides extremely 
            smooth. <br>
            <br>
            Sculpted lines, distinctive grille and projector beam headlights add 
            a decidedly international flavour to the Epica. But the elegance 
            doesn’t stop there. Available options include a power sunroof 
            (standard on LT), 16&quot; alloy wheels and low-profile tyres for 
            enhanced handling. And the LT model features chrome body-side 
            mouldings and leather appointments inside.<br>
            <br>
            <b>The interiors</b><br>
            Keyless remote is standard on the Epica. A button on the key, not on 
            a separate key fob, unlocks all the doors with the first press. For 
            safety, most new cars have remote keys which open the driver’s door 
            with one press, and all the other doors with the second press. This 
            is something that future Epica’s should address.<br>
            <br>
            Inside, the Epica has an attractive interior that is generally 
            user-friendly. The 8-way cloth power driver’s seat with height 
            adjustment, seat cushion tilt, and recline is comfortable, and wide 
            foot-wells and a wide ‘dead’ pedal provide plenty of foot-room. The 
            ignition key receptacle is on the dash beside the steering wheel, 
            not on the steering column. <br>
            <br>
            The interior includes soft, fabric-covered seats, cloth door inserts 
            and cloth seats, two-tone textured dash plastic, and warm, 
            artificial walnut trim on the console. The three gauges include a 
            tachometer, speedo and coolant/fuel/ transmission indicator. Stalk 
            and dash controls are easy to see and use. The standard AM/FM/CD, 
            with optional MP3 player, has a bright green display, which is easy 
            to read. The sound quality is groovy. Two cup-holders reside under a 
            flip-up cover, and in the rear, two cup-holders and storage are 
            contained in the fold-down centre armrest. <br>
            <br>
            The rear bench has adequate headroom and legroom. The raised front 
            seat cushions allow generous foot-room for the rear passengers, and 
            there are two rear height-adjustable head restraints. The 13.4 cubic 
            feet trunk is adequate and offers 60/40 split folding rear seats for 
            longer items. There is a remote power trunk release on the ignition 
            key.<br>
            <br>
            <b>The performance</b><br>
            The Epica’s all-aluminium 2.5-litre inline six-cylinder engine with 
            four valves per cylinder, twin overhead cams, and variable intake 
            induction, is mounted sideways in the front driving the front 
            wheels. Under acceleration, it revs smoothly and evenly with little 
            noise or vibration. Its 160 HP and 240 lb-ft of torque is more than 
            adequate for everyday driving needs and you won’t have to worry 
            about torque-steer. <br>
            <br>
            On the freeway, the engine turns over a comfortable, vibration-free 
            2300 at 100 km/h, and 2800 at 120 km/h in top gear. Kudos must also 
            go to the Epica’s smooth-changing four-speed automatic transmission. 
            The transmission adjusts automatically for uphill or downhill 
            driving by holding in gear. The only thing I wasn’t comfortable with 
            was its irregular, stainless steel shift gate, which makes it 
            difficult to manoeuvre the gear lever without some effort. However, 
            the lever allows the driver to pull straight back from D to 3 for 
            downshifting on hills. <br>
            <br>
            The Epica’s suspension is soft. That means more lean in the corners, 
            but it also means you don’t have to suffer every time you hit 
            potholes and cracked pavement, of which there seems to be 
            increasingly more of nowadays. The 15-inch tyres are standard on LS 
            models, but my car had the optional 16-inch tyres, which provided 
            good all-around wet (it rained last month, remember?) and dry 
            performance, and were noiseless on the freeway.<br>
            <br>
            <b>Safety</b><br>
            The Epica’s agility and comfortable ride are partly due to its fully 
            independent suspension (front double wishbone/ rear multi-link). And 
            its speed sensitive rack and pinion power steering is nicely 
            weighted for easy operation at all speeds. The Epica’s turning 
            circle is just 10.4 metres (34.1 ft). This is all the more 
            remarkable when you consider the straight six under the hood is 
            mounted sideways, leaving less room for the front wheels to turn. 
            Brakes are standard discs at all four corners, but ABS is standard 
            on LS models. The Epica’s 137 feet braking distance is considered 
            good in its class. The car’s unit-body is tight and rattle-free.<br>
            <br>
            <b>The finishing line</b><br>
            The Epica is much more than an elegant face. A 160 hp 2.5 L DOHC 
            inline 6-cylinder engine, four-wheel independent suspension, 
            four-wheel disc brakes, speed-sensitive power steering and 
            four-speed automatic transmission with gated shifter are all 
            standard. Which means you can expect the response, handling and 
            smooth ride of a luxury car, for a much more comfortable price. The 
            Chevrolet Epica engine achieves 11.6L/100 km in the city and 
            7.8L/100 km on the highway.<br>
            <br>
