<p align="justify"><font face="Verdana" size="1">
<b>HE Eng. Mohammed bin Ali Al Wohaibi, executive president of Omantel, speaks 
to Vicky Kapur about his childhood, management style and why Omantel needs to 
undergo a mindset change to succeed in the post-WTO era</b><br>
<br>
<img border="0" src="profile.jpg" align="left" hspace="6" width="150" height="201">Born 
into a family of fishermen, he used to accompany his father on fishing trips 
while he was still a child. Eldest among three brothers and six sisters, he 
spent his childhood playing soccer and attending secondary school in Sidab 
(Muscat). ‘‘My father wasn’t educated, and so wasn’t my mother,’’ he reveals. 
‘‘But I was fond of reading since I was very young,’’ he adds. Meet HE Eng. 
Mohammed bin Ali Al Wohaibi, the executive president of Oman Telecommunications 
Company (Omantel). <br>
<br>
The unassuming HE Al Wohaibi is a graduate in electronic and telecommunications 
engineering from the University of Toledo in Ohio, US. ‘‘After graduating in 
1989, I came back to Oman and started working in a government agency,’’ he says. 
‘‘Then, in 1992, I got married and went back to the US to complete my masters in 
science and telecommunications,’’ he adds. He did his masters from the 
University of Pittsburgh, Pennsylvania.<br>
<br>
He joined the same government agency on his return in 1994. HE Al Wohaibi joined 
Omantel in 2002 as its executive president, and even though it is still a 
monopoly, he has managed to take the company to respectable heights of 
operational excellence. <br>
<br>
<b>Dexterous delegator</b><br>
‘‘I believe delegation is very important,’’ declares HE Al Wohaibi. ‘‘You allow 
your managers to do their tasks, and hold them accountable for their actions,’’ 
he states. However, he has a word of caution for the compulsive delegator – 
‘‘Delegation doesn’t work without proper training and education. You need to 
train your people—both in management as well as in their technical areas of 
speciality—before you think of delegating any task of consequence,’’ he warns. 
And, of course, give them ample freedom and opportunity to perform. ‘‘With 
proper guidance and mentoring, delegation works pretty much,’’ he claims.<br>
<br>
HE Al Wohaibi is clear about the role that he is expected to play as the man 
in-charge of affairs at the State-owned Omantel. ‘‘I believe that the head of an 
organisation should play more of a facilitative role, as opposed to a commanding 
role,’’ he affirms. ‘‘We should create an environment and culture in our 
organisation that fosters proper decision-making and disseminate information 
across the hierarchical boundaries of the organisation,’’ he advises. <br>
<br>
That, according to him, helps in informed decision-making by employees ‘‘who 
would be aware of what’s happening throughout the organisation’’ at any given 
point in time. ‘‘We apply this new culture in Omantel,’’ he reveals. ‘‘We are 
trying to gradually move it from a government bureaucracy, where the focus was 
more on processes and procedures, to a more professional, business-like culture 
where more emphasis is given on customer service, faster decision-making, and 
operational efficiency’’. <br>
<br>
<b>The challenge of change</b><br>
Changing the culture of any organisation is not an easy task, least of all when 
it is a government-owned monolith that has practically no incentive to improve. 
‘‘It is a daunting task – it’s easier said than done,’’ agrees HE Al Wohaibi. 
‘‘An organisation that has been doing certain things in a certain way since the 
past 30 years builds its own culture. To come and change that culture in a short 
time is, indeed, extremely difficult.’’ <br>
<br>
HE Al Wohaibi, nevertheless, is unfazed in his relentless pursuit of operational 
efficiency. Says he: ‘‘We are concentrating on young managers as we want to 
capture new thinking and move forward in a direction where others can follow.’’ 
In order to achieve that, HE Al Wohaibi has implemented the idea of bringing 
young people from outside, with different training and cultural backgrounds, 
with the same education level to join as managers in his organisation. <br>
<br>
‘‘That has proved to be a very useful step,’’ he claims. ‘‘These people bring 
their own thinking and culture into the organisation, and things that were 
previously thought to be impossible by our managers were actually made reality 
by the new entrants.’’ And that not only challenged the existing culture, it 
actually created internal competitiveness among the managers, which, of course, 
is healthy. <br>
<br>
<b>Clarion call</b><br>
The man who introduced competitiveness within his organisation welcomes outside 
competition as well. ‘‘Introduction of competition will make us move faster,’’ 
he says of the impending liberalisation of the fixed and Internet sectors of the 
Sultanate’s telecom market. ‘‘While competition is, of course, a threat, in 
general, we feel that it is a good thing. It’s an opportunity that we are 
embracing and we are moving forward.’’<br>
<br>
In fact, HE Al Wohaibi has been one of the proponents of competition ever since 
Oman acceded to the World Trade Organisation. ‘‘We think that this (opening up 
of the country’s telecom sector for competition) should have happened a while 
back. If it had happened long back, we would have been in a much better shape 
now.’’ <br>
<br>
Talking about the challenges that the State-owned Omantel and Oman Mobile face 
today, HE Al Wohaibi says: ‘‘The first and the most bitter challenge is the 
competition itself, where government companies are faced with competition 
against companies with international experience. This is the case with Oman 
Mobile and Nawras.’’<br>
<br>
The second challenge, according to HE Al Wohaibi, lies in trying to win the 
hearts and minds of the consumers. ‘‘Suddenly, the customer will have a 
choice,’’ he explains. ‘‘Both Omantel and Oman Mobile have to change their 
thinking, their mindsets – from being utility-based companies to more 
competitive business-like customer-focused companies,’’ he advises. ‘‘So, where, 
in the past products and services were designed and launched based on technical 
requirements, from now on, they will have to be actually customer-driven,’’ he 
adds. <br>
<br>
And that, according to him, is the biggest challenge for government companies. 
‘‘This is difficult for the operators because they don’t have the internal 
expertise for the simple reason that they haven’t built it throughout the years. 
They may have a very strong technical team, but they don’t have a very strong 
customer focus. This is where we seek international experience – we want to hire 
international experts and consultants to help us restructure our company to be 
more competitive and have a better customer focus,’’ he says. ‘‘In the last two 
years, we have come a long way. It is not easy to transform. However, we have 
had some success on the customer front, but we still have a long way to go.’’
<br>
<br>
HE Al Wohaibi sees the announced part-privatisation of Omantel in March 2005 as 
another challenge, where Omantel will have to act as a public company with 
public shareholders, and will have to report to the CMA on a regular basis, have 
to be transparent in its transactions, and will have to monitor its image as 
that will have an impact on its share value. ‘‘We have to suddenly start doing 
so many things that we are not used to doing as of now,’’ he says. 
Interestingly, just like liberalisation, HE Al Wohaibi believes privatisation 
too could have happened earlier. <br>
<br>
‘‘Privatisation is also something that should have happened by now. We think 
that the introduction of Omantel on the stock market will have a positive impact 
on the market. As management, we are looking forward to the company getting 
listed on the Muscat Securities Market, because we think that that will create a 
de facto business culture that we have to willy-nilly adopt. Currently, we don’t 
really completely act like a business concern. We try to act like a private 
company, but we are fully owned by the government, and the stakeholders have a 
say in what we do or don’t do. Despite that, I think what we have done is 
impressive.’’<br>
<br>
<b>Customer service</b><br>
If liberalisation and part-privatisation do deliver single-minded customer focus 
to Omantel, HE Al Wohaibi will be a happy man. He has, in fact, tried to take a 
two-pronged approach to customer satisfaction. While on the one hand Omantel is 
improving its network efficiency, on the other, it is slowly but surely getting 
its tariff structure in line with global trends. Moreover, the company is also 
trying to ease the payment modes, thus making the whole experience of bill 
payments ‘‘less painful’’. <br>
<br>
‘‘Our customers have always been satisfied with our service, but we did receive 
some complaints about the bill collection. We are trying to make it as painless 
as possible. We are trying to introduce different modes of payment – kiosks, 
bill payment machines, signing up with banks to avail any-time bill payment 
through ATMs etc. So it’s going to be more easy to pay,’’ claims HE Al Wohaibi.
<br>
<br>
As far as reducing tariffs further is concerned, Omantel is looking at the 
possibility of slashing international tariffs. ‘‘If approved, we will soon 
announce a reduction in international tariffs. I also understand that Oman 
Mobile is in talks with the regulator to introduce lower tariffs in mobile 
telephony as well,’’ he reveals.<br>
<br>
<b>Personal front</b><br>
As a child, HE Al Wohaibi used to be heavily into soccer, which he had to give 
up after he broke his leg and was bed-ridden for a ‘‘very long time.’’ His 
favourite pastimes now include brushing up his management skills by reading the 
likes of Michael Porter. His favourite holiday destination within Oman is the 
desert. ‘‘I love dune-bashing,’’ he exclaims. ‘‘We have two types of trips – one 
with friends, during which we visit the ‘tougher’ areas, usually on weekends. 
Then we have family trips, where we go to the countryside, which is also on a 
regular basis.’’<br>
<br>
Outside of Oman, he doesn’t have a ‘favourite’ holiday destination since he 
hasn’t ‘‘been to a place twice.’’ Besides, with the country’s telecom sector in 
the midst of a churning, ‘‘it’s difficult to take off.’’ However, he still 
manages to sneak two breaks a year – one short and the other long – to recharge 
his batteries and avoid cross-connections! </font></p>

