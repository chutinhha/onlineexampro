<p align="justify"><font face="Verdana" size="1"><b>Oman Authorities intensify 
campaign to protect IPR laws</b><br>
<img border="0" src="im23.jpg" align="left" hspace="6" vspace="3" width="150" height="104">In 
yet another demonstration of its tough stance against violators of Intellectual 
Property Rights (IPR), authorities in Oman recently raided four resellers and 
seized copies of pirated software from their premises.<br>
The raids were conducted by the Ministry of Heritage and Culture, Oman, in 
collaboration with Business Software Alliance (BSA), the global authority that 
works to safeguard the IPR of software developers. <br>
‘‘Oman strongly believes that protection of intellectual property rights is the 
cornerstone of modern business. We have introduced IPR laws that provide full 
protection to software developers and are determined to follow up the 
legislation with campaigns, including raids of violators. The latest raid 
assures our commitment to protect IPR under Omani Laws,’’ said Khalid Al 
Ghassani, general manager, Directorate of Culture, Ministry of Heritage and 
Culture, Oman.<br>
Jawad Al Redha, Co-Chairman, BSA Middle East said: ‘‘The latest action by Omani 
authorities should serve as yet another warning to resellers that Oman will not 
tolerate violation of intellectual property rights. We are encouraged by this 
move and would like to work in close coordination with the authorities to carry 
out relentless campaigns so that we can mutually reach the goal of reducing 
piracy in the general interest of the software industry and the national 
economy.’’<br>
‘‘We would like to commend the authorities in Oman for their serious approach to 
piracy reduction,’’ added Al Redha. ‘‘Software piracy poses a serious threat to 
the development of the software industry, because it de-motivates software 
developers from offering their products to markets that have high levels of 
piracy. Countries that show a strong commitment to combating software piracy 
stand to gain in the long run, because there would be a high degree of trust 
from software developers who would expand their operations and offer employment 
opportunities to the local population as well as offer competitive pricing.’’</font></p>
