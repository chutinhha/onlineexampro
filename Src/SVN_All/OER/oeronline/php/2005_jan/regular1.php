<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Improving 
credit quality</font></font><font face="Verdana" size="1"><br>
<br>
<b>Omani banks ought to apply sophisticated tools to sustain good quality credit 
and improve deteriorating quality lending, writes Ali Hassan Moosa</b><br>
<br>
<img border="0" src="im80.jpg" align="left" hspace="6" width="125" height="147">Lending represents the heart of the banking 
industry. For financial intermediaries, credit extended to individuals and 
corporates alike generates the largest share of operating income. The increased 
competition between commercial banks, specialised banks and leasing companies 
has lead to changes in lending policies and loan portfolio composition.<br>
<br>
As of 2003 year-end, locally incorporated banks’ total lending amounted to 
approximately 72 per cent of total bank assets. This percentage is on the higher 
side if compared to banks in more developed economies. Furthermore, some banks 
are already near the regulatory ceiling for personal lending of 42.5 per cent of 
loan portfolio.<br>
<br>
Therefore, many bankers and economists alike argue that, in the local economy, 
one of the greatest challenges that financial intermediaries face today is 
improving credit quality for their lending activities. In the past few years, a 
number of banks and leasing companies had gone through a consolidation process 
in order to improve the quality of their loan portfolios.<br>
<br>
A key method of ensuring credit quality is through monitoring exposures. The 
credit portfolio must be sliced-and-diced in all sorts of views in order to 
assess exposures and take remedial action wherever necessary. <br>
<br>
Portfolio analysis can be done in a number of ways, such as; lending by industry 
segment, lending by employer, lending by purpose, limits monitoring for 
individual and corporate customers, age-wise analysis, etc. The portfolio 
analysis can also be applied to non-performing loans in order to identify 
correlations.<br>
<br>
Credit bureaus are strategic enablers for the growth and quality of consumer 
credit portfolios while also protecting the privacy and credit exposures of 
individual consumers. Currently in Oman, one credit bureau exists that is 
operated by the Central Bank of Oman (CBO). The entity is named Banking Credit 
Statistics Bureau (BCSB) and provides limit and exposure data as well as 
non-performing loan snapshots.<br>
<br>
The BCSB is currently in transformation in order to provide more accurate data 
to banks and other financial intermediaries in the Sultanate. It would be 
immensely useful should the bureau incorporate a credit scoring mechanism as 
well as collaborating with other regional bureaus in order to consolidate 
regional data, most importantly within the Gulf Cooperation Council (GCC). This 
is particularly important in the light of GCC monetary integration.<br>
<br>
In order to assist banks minimise loan losses, CBO had placed stringent 
reporting definitions on all banks and leasing companies in Oman. One example 
that truly helps banks monitor credit quality of borrowers on an ongoing basis 
is the requirement to obtain, at least once a year, copies of full-set financial 
accounts of borrowers above a certain lending limit (RO250,000 from a single 
institution or RO5,000,000 combined from all financial intermediaries in the 
Sultanate).<br>
<br>
Banks in Oman need to start applying sophisticated tools that, on the one hand, 
help them sustain good quality credit and, on the other, improve deteriorating 
quality lending. Such tools and the human expertise that go with it can be 
imported to the country, if not available locally.<br>
<br>
Credit scoring is yet to be introduced in Oman. Credit scoring is a statistical 
process that assigns a score to a borrower based on the characteristics of the 
borrower that indicate a high or low likelihood of loan repayment. <br>
<br>
The technique should improve the accuracy and consistency of credit decision 
through the application of well-defined parameters and automated processes. By 
applying credit scoring, banks can deal with a large number of credit 
applications without compromising on credit quality, since credit scoring 
standardises the perceived quality of different types of loans.<br>
<br>
Another method that banks could apply to improve credit quality is by being more 
proactive in managing high-risk customers. To start with, banks must establish 
the criteria for identifying such customers. This process requires the use of a 
combined approach of market intelligence/customer referrals and modern 
transaction risk analytics. A simple form of the latter is the analysis of 
repayment history.<br>
<br>
The second step is to manage identified customers by coercing them to decrease 
bank exposure. The bank should always start with a gentle approach, as target 
customers may not appreciate why they have been singled out. The trick is to 
convince high-risk customers that the bank is working with them (and not against 
them) to help them manage their finances. Should such efforts be successful, 
high-risk customers will turn into the biggest supports for those banks that 
helped them through rough times.<br>
<br>
Credit quality in banks can be measured through a number of key performance 
indicators (KPIs). Examples include the following ratios:<br>
(i) Loan loss provisions to total operational income.<br>
(ii) Loan loss provisions to average loans.<br>
(iii) Non-performing loans to gross loans.<br>
(iv) Loan loss reserve to gross loans.<br>
(v) Non-performing loans coverage.</font></p>

<p align="justify"><font face="Verdana" size="1">Comparison of the above indicators between different industry segments can be 
tricky. Since the fundamental business model is different; leasing companies, 
mortgage banks, and commercial banks will all have different norms for these 
KPIs.<br>
<br>
A number of local banks are currently at various stages in their balance sheet 
clean-up exercise. Credit quality programs were the result of increased bad 
debts in the local economy as non-performing loan percentages to gross loans had 
risen sharply from an average of 8 per cent in 2001 to an average of 12 per cent 
in 2003. <br>
<br>
The table below illustrates some credit quality indicators for selected banks in 
Oman based on a research that was recently conducted by the Kuwait-based Global 
Investment House:<br>
<br>
Mortgage banks normally operate at the lowest end of the credit risk spectrum as 
values of securities/collaterals held by such banks may be in excess of their 
lending portfolios. Mortgage banks in Oman secure their lending in a number of 
ways such as first lien mortgage registered officially at the Ministry of 
Housing, secured source of repayment, and insurance covers that protect the 
interest of customers and banks alike.<br>
<br>
In comparison, leasing companies operate in a volatile environment where risks 
are much greater than for other types of financial intermediaries. It is 
unfortunate to note that the local leasing industry is mainly focused on the 
relatively risky consumer lending, specifically auto-financing, rather than 
infrastructure project financing that earns greater wealth to the national 
economy. <br>
<br>
A well-defined periodic credit review process is of paramount importance in 
measuring credit quality. Effective credit risk management separates loan review 
from credit analysis, execution, and administration. The review process itself 
can be divided into two functions; (a) monitoring the performance of existing 
loans, and (b) handling problem loans. <br>
<br>
Banks in Oman are encouraged to set up independent loan review processes that 
comprise of formal committees that include personnel other than the sanctioning 
individuals/committee. Furthermore, Internal Audit’s involvement in the process 
is crucial to ensure that credit quality is not compromised in any way.</font></p>

