            <br>
            </font><b><font face="Verdana" size="1">Increase in transparency is 
            an important factor that will characterise the future roles of the 
            Ministry of Manpower in its formulation and application of manpower 
            policies, says:<br>
            <br>
            <img border="0" src="im11.jpg" align="left" width="150" height="165">His 
