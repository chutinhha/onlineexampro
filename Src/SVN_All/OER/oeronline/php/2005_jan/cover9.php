            Minister of Manpower</font></b></p>
            <p align="justify"><font face="Verdana" size="1">The blessed Omani 
            renaissance led by His Majesty Sultan Qaboos Bin Said accomplished 
            remarkable progress in the economy and society at large thanks to 
            the inspired and wise leadership of His Majesty, and to his keen 
            commitment to establish a modern state, and to attain social 
            progress combined with sustainable development. <br>
            <br>
            The high rates of economic growth achieved in the last decades paved 
            the road for further economic progress, modernisation, and that of 
            Human Resource (HR) Development and social transformation based upon 
            higher living standards and better life quality for the Omani 
            citizens. <br>
            <br>
            This judgement is based upon the grand achievements of the past 
            three decades as the national economy grew rapidly, attaining an 
            average annual growth rate of 8.4 per cent in the 1980s and 5.9 per 
            cent in the 1990s. Such achievement has led to the attainment of 
            wide-ranging economic and social goals, including the vast 
            achievement in the field of HRD, as well as the level of total 
            employment and that of job creation records in almost all sectors of 
            the economy, particularly in the private sector. <br>
            <br>
            Prior to the launching of the Futuristic View for the Oman Economy: 
            Oman Vision 2020, and with regard to HR, the period from 1970 to 
            1995 witnessed the following changes:<br>
            (1) An increase in the total number of students enrolled in schools 
            from 909 in 1970 to 501,300 in 1995.<br>
            (2) An increase in the number of the labour force employed in the 
            Omani economy from 187,100 in 1976 (67.8% Omanis), to 776,200 (35.2% 
            Omanis).<br>
            (3) Life expectancy at birth increased from 49.3 years in 1970 to 
            67.4 years in 1995. At the same time, infant mortality declined from 
            64 per thousand in 1980 to 20 per thousand in 1995.<br>
            (4) Rapid improvement in the quality of life and the standard of 
            living, as the individual share in private final consumption 
            increased from RO200 in 1976 to RO1,204 in 1995.<br>
            (5) A diversified structure of manpower supply was associated with 
            the rapid expansion of general education, vocational training, 
            technical education and that of university education.<br>
            <br>
            Building upon the achievement during the period of 1970 to 1995, and 
            what was attained in terms of economic growth, economic 
            diversification, changes in sectoral employment, improvement in the 
            coverage of education and training, and improvement in the standard 
            of living, a perspective strategic view for the Omani economy up to 
            the year 2020 was launched with basic strategies directed towards:<br>
            1. HR Development<br>
            2. Economic Diversification<br>
            3. Private Sector Development<br>
            <br>
            Benefiting from previous experience and taking into consideration 
            the impact on development of globalisation, trade liberalisaton, the 
            rapid advancement of communications, know-how combined with the 
            advancement of technology, it was realised that for Oman to achieve 
            further growth with sustainable development, the economy should be 
            diversified, the private sector should be promoted, and manpower 
            quality should be enhanced.<br>
            <br>
            And for the private sector to acquire modern production techniques 
            with high quality competitive products, there is a clear need for a 
            quality manpower capable of absorbing the new technologies with 
            clear ability to participate fully and efficiently in the production 
            process. <br>
            <br>
            Considering current and future prospects of the domestic labour 
            market, the issues of manpower supply and demand, it is of vital 
            importance to emphasise the following indicators and factors:<br>
            1. The final results of Census 2003 shows that the Sultanate’s total 
            population reached 2.34 million (76.1 per cent Omanis and 23.9 per 
            cent expatriates). <br>
            2. It is expected that the Sultanate’s total population would reach 
            about 4.1 million in the year 2015.<br>
            3. Forty-three per cent of the Omani population is below the age of 
            15, and the Omani population participation rate in economic activity 
            is 24 per cent. Accordingly, the dependency ratio is 1:4.<br>
            4. Information revealed by the Ministry of National Economy with 
            regard to manpower statistics in the private sector shows that the 
            total number of employees in that sector was as follows:<br>
            <br>
            <img border="0" src="im10.jpg" width="196" height="80"><br>
            <br>
            The above information indicates that:<br>
            • Omanisation in the private sector increased from 16.6 per cent in 
            the year 2000 to 18.7 per cent in 2003.<br>
            • The private sector demand for manpower grew by 6.4 per cent; it 
            grew by 10.8 per cent for the national manpower, and 5.4 per cent 
            for expatriate labourers.<br>
            <br>
            With regard to economic growth, the International Monetary Fund (IMF) 
            report shows that the Omani economy grew in real terms by 7.5 per 
            cent in 2001, 1.7 per cent 2002, and 1.5 per cent in 2003; its 
            forecast shows that the Omani economy will grow in real terms by 
            2.5, 3.6 and 5.3 per cent in 2004, 2005 and 2006, respectively.<br>
            <br>
            With these developments in mind, it is important to emphasise that 
            current and future development strategy considers the private sector 
            as a leading sector for future development, as well as a major 
            source of employment opportunities for the rapidly growing national 
            manpower.<br>
            <br>
            To achieve that with regard to HR development, certain actions were 
            taken in close cooperation between the government and the private 
            sector; two national seminars were held with basic concentration on 
            the promotion of national manpower employment, the first in 2001, 
            and the second in 2003; Sanad programme was established for the 
            purpose of promoting self-employment, and a Joint Sectoral Committee 
            for Omanisation was established to deal specifically with national 
            manpower employment issues. This joint committee has more than 90 
            per cent private sector representation in its membership.<br>
            <br>
            In addition, certain measures were taken to improve education and to 
            develop vocational training and technical education; these include:
            <br>
            1. <b>The development of general education</b> with the 
            establishments of a modern curriculum for basic education that meets 
            the demands of the labour market and the knowledge economy needs.<br>
            2. <b>Strengthening higher education</b> and enhancing the role of 
            the private sector in higher education, with emphasis upon achieving 
            linkages between theoretical learning and labour market practical 
            needs.<br>
            3. <b>Developing technical education</b> to make it possible for the 
            Higher College of Technology and the Colleges of Technology to 
            deliver knowledge and skills required by the knowledge economy as 
            stipulated by Oman’s economic Vision 2020. To achieve that, a new 
            program is now in application in all the technical colleges with 
            basic criteria for the students’ competence to achieve the program 
            goals of providing the labour market with a competent, highly 
            skilled and productive labour force. Twenty-eight specialisations 
            are now available in the colleges, including those in the fields of 
            engineering, applied sciences, business, and IT. The technical 
            education program has been developed in cooperation with the private 
            sector and with direct involvement from leading international 
            institutions and technical universities who has remarkable 
            experience in this field. <br>
            4. <b>Vocational training reform</b> in both the public sector and 
            the private sector training providers is designed to make it 
            possible for them to provide the labour market with the types and 
            the levels of skills that will yield the sort of manpower supply 
            capable of participating effectively in the Omanisation process.<br>
            5. <b>Introducing labour market measures</b> that will help the 
            private sector establishments to get the skills and the manpower 
            required to meet their needs.<br>
            6. <b>A new labour law</b> issued by Royal Decree (35/2003) is now 
            in application (since June 2003); the provision of the labour law 
            improves conditions of employment and allows for further cooperation 
            between the tripartite partners in the labour market – the 
            government, the employers and the workers. <br>
            <br>
            <b>The future vision of the HRD in the of the Sultanate goes hand in 
            hand with the economic strategy, aiming at:</b><br>
            (a) Strengthening the role of the private sector in the development 
            process. <br>
            (b) Achieving economic diversification.<br>
            (c) The creation of employment opportunities capable of absorbing 
            the future supply of the national <br>
            manpower.<br>
            <br>
            <b>Related to that, the basic features of the Sultanate’s HR 
            development strategy include the following:</b><br>
            1. To sustain and to strengthen further the real partnership 
            achieved in recent years between the government and the private 
            sector in the formulation of the manpower policy; this includes 
            education, vocational training, technical education, higher 
            education, and employment policy.<br>
            Increase in transparency is an important factor that will 
            characterise the future roles of the Ministry of Manpower in its 
            formulation and application of manpower policies; this includes the 
            promotion of vocational training, technical education, cooperation 
            with the private sector in employment location, and labour market 
            polices and regulations.<br>
            <br>
            2. To work for the promotion of vocational training and technical 
            education that will provide the private sector with the type of 
            skills appropriate to their needs. Major emphasis is placed upon 
            on-the-job training, where private sector participation would be of 
            vital importance so as to provide the private establishments with 
            the skilled manpower capable of participating effectively to the 
            Omanisation and the productivity process.<br>
            Partnership in training and employment would help, on the one hand, 
            the Omani youth to gain technical and professional skills that is 
            employable and productive; and at the same time, it would help 
            employers to rationalise their dependence upon expatriates, and to 
            allow skilled national manpower to participate fully in the 
            development of their national economy and to benefit from its gains.<br>
            <br>
            3. To set up occupational standards and to develop occupational 
            profile, 2005 will witness the establishment of the Occupational 
            Standards and Skill Testing Centre. This would help in: <br>
            • Upgrading the skills of the Omani manpower and develop national 
            HR.<br>
            • The transformation of the vocational training system towards 
            market needs.<br>
            • Serving as benchmark for the performance of trainees (and 
            employees) with regard to job requirements in employment and 
            Omanisation process.<br>
            • Assuring the private sector employers that the results of the 
            training process are relevant and meet quality standards.<br>
            • Rationalising the use of foreign workers in the national economy, 
            and to provide a technical base for developing tests that would help 
            companies with demand for qualified manpower to acquire the right 
            type of workers with the needed competencies, whether from domestic 
            or external sources.<br>
            • Establishing a national Qualifications Framework that would lead 
            to the further development of the system of vocational training and 
            technical education in the Sultanate.<br>
            • Serving as a guideline for the development of all curricula 
            applied in the technical colleges, as well as in private and public 
            training institutes. <br>
            4. A career guidance centre will be established under the umbrella 
            of the Ministry of Education for the purpose of creating an 
            appropriate link between the schools, training institutions, 
            technical colleges, universities and the labour market. This centre 
            would provide the required tools whereby young Omanis can be 
            assessed, guided and supported to be integrated with labour market 
            needs.<br>
            <br>
            5. To diversify the structure of vocational training and technical 
            education, and their fields of specialisations and skill levels so 
            as to meet current and future manpower needs of the industry.<br>
            <br>
            6. To create and to maintain a flexible labour market that would 
            help the national economy capacity, to increase its productivity, 
            and to sustain progress and competence with regard to global 
            economy.<br>
            <br>
            7. To accelerate privatisation, and to encourage self-employment 
            through the creation of small businesses and medium enterprises that 
            can help in providing more jobs and employment opportunities for new 
            entrants to the domestic labour market.<br>
            <br>
            <b>The national development strategy realises that:</b><br>
            a) Education and skills of the labour force is one of the key 
            elements for productivity and growth, and companies investing in 
            Oman should have a clear access to the skills they need for their 
            production process.<br>
            b) Highly skilled, well–trained and well-educated manpower can 
            ensure higher levels of productivity in all economic activities.<br>
            c) Transparency, flexibility, and mutual cooperation between the 
            development partners is a basic condition that can make our HR 
            development strategy functional and beneficial.<br>
            The Sultanate’s future vision for HR development strategy is aimed 
            at achieving productivity, enhancing competence and development, 
            gaining prosperity and attaining economic growth with sustainable 
            development.</font></p>
