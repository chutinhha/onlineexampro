            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im2.jpg" align="left" hspace="5" width="175" height="157">Forewarned is 
            forearmed. Okay, I could not resist the temptation to begin with a 
            cliché, but the fact remains that accurate and timely information 
            about what is likely to happen to the economy and society in the 
            future has always been of value to business decision-makers. <br>
            <br>
            Forecasting has become an important part of planning for any other 
            business. For example, sales forecasts impact the inventory of both 
            finished goods and raw materials, the need for some types of 
            personnel, space requirements, and financing, among other things. 
            Macroeconomic forecasts may influence whether and where a business 
            decides to expand, and the type and amount of financing that is 
            used.<br>
            <br>
            One of the key assumptions for most forecasters is that the past 
            serves as the most important guide to the future. That does not mean 
            that the future is a re-run of the past or that data about the past 
            should be the only basis for a forecast. Obviously, socio-economic 
            conditions and global economies do not remain constant over time. 
            Nevertheless, data about past trends and activities, when fit into a 
            theoretical framework, provide some of the best information 
            available.<br>
            <br>
            Any business operates on the basis of some forecast about the 
            future, whether it is an intuitive guess or a sophisticated model 
            involving large amounts of quantitative data. Perhaps in some small, 
            stable businesses, the former may work reasonably well, but chances 
            are, the more thoughtful and thorough the forecasting process, the 
            more useful it will be. <br>
            <br>
            In the first issue of the New Year, OER invites Oman’s industry 
            leaders to reflect on the previous year’s achievements and provide 
            an insight into what 2005 may entail for their particular domain and 
            analyse the trends in it. These blueprints of the future carry a lot 
            of weight as they are being proffered by none other than the 
            architects of the future, the ministers in charge of taking forward 
            their particular wheel in the vehicle that is Oman. <br>
            <br>
            That most of these leaders believe in the country’s capability to 
            overcome any challenges bound to be thrown by the future, and the 
            fact that all of them speak the same language of cautious optimism 
            is proof that this machinery is working in-sync, with the 
            government—the engine of Oman—well in control. In their write-ups, 
            these industry architects briefly reflect upon the growth of their 
            particular sectors since their inception in Oman, benchmark the 
            industry against that in the region and also globally, and identify 
            the key trends that will shape business in 2005 and beyond.<br>
            <br>
            In its endeavour to provide the decision-makers with meaningful 
            insights into the various economic sectors of Oman and the data 
            required to take an informed decision, we hope that this special 
            issue will be used as a reference point for entrepreneurs and 
            businessmen for investing in Oman’s economy. <br>
            <br>
            In such dynamic and unpredictable times, forecasts can only be 
            written in sand – and not carved in stone. All predictions, 
            prophecies and forecasts made in today’s age will have one thing 
            common – all of them will prove to be at least partly wrong! 
            However, we hope that when carefully selected and properly applied, 
            OER’s series of forecasts will help Omani businesses move 
            confidently into the future.<br>
            <br>
            The industry leaders’ forecasts—in alphabetical order by 
            sector—follow the national economic overview provided by His 
            Excellency Ahmed bin Abdulnabi Macki, the minister of National 
            Economy and Deputy Chairman of Financial Affairs &amp; Energy Resources 
            Council, and are concluded by the global economic outlook by David 
            Bloom, Director (Strategy), Global Markets, HSBC Bank, London.</font></p>
