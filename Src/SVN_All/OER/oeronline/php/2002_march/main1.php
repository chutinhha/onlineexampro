                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">All for one and one for all</font><p><font face="Verdana" size="1"><b>Does the Gulf Really Need a Single Currency?<br>
                    Preparations are underway for the launch of a GCC common currency by 2010. But will the Gulf really benefit from a monetary union? Some experts doubt there is a great deal to be gained. Shilpa Mathai reports</b></font><font face="Verdana" size="2"><br>

                    <br>

                    <img border="0" src="../../images/2002_march/17.jpg" align="left" hspace="5" vspace="5" width="225" height="269">Currency unions are definitely the flavour of the month. The smooth launch of the euro has nudged other nations to consider forming monetary blocs - the Association of South East Asian Nations, New Zealand and Australia, and the Latin American countries are debating the advantages of economic integration.&nbsp;<br>
                    Ever since the Gulf Cooperation Council (GCC) realised the need to establish a regional common market to improve their trading position, the six member states have been edging towards a common Gulf currency. At a GCC leaders' meeting in Muscat late last year, the states set a timetable to unify their currencies by 2010.&nbsp;<br>
                    To achieve a single currency, the GCC economies require a similar set of targets to the Maastricht criteria that prevail in Euro land.&nbsp;<br>
                    Suresh Kumar, General Manager at Emirates Financial Services in Dubai, says there are already a lot of similarities between the GCC economies.<br>
                    "Though there are diversifications between the GCC countries, they are reasonably homogeneous as the single largest segment within their economies is petroleum and gas," he says.<br>
                    "If one were to apply the convergence criteria adopted for the European Union, then there is considerable similarity in terms of fiscal deficits/surplus, GDP size, per capita income and the level of foreign exchange reserves supporting the currency."<br>
                    The foundations for a successful single currency also exist in more general terms. Monetary policy differences are less relevant in the GCC than in Europe as most currencies, except the Kuwaiti dinar, are linked to the US dollar; and GCC countries tend to control their economies through fiscal means because of their high reliance on oil revenue.&nbsp;<br>
                    Horst Kohler, Managing Director of the International Monetary Fund, believes it is vital that the Gulf States accelerate their move towards unified customs tariffs and a common currency in order to attract more foreign direct investment. Investors tend to view the region as a single entity for investment, and it is a priority that the region is politically and economically stable. Kohler says a single currency with an independent central bank would result in more transparent economic decision-making.&nbsp;<br>
                    Other oft-cited benefits of a common currency are that it would eliminate disruptions to trade and investment caused by exchange rate adjustments. Interest rates would be stabilised and Gulf bourses would be strengthened, adding liquidity and depth to the financial and banking sectors.&nbsp;<br>
                    <br>
                    </font><b><font face="Verdana" size="1">MONETARY UNION NO CINCH</font></b><font face="Verdana" size="2"><br>
                    But despite the similarities between the GCC economies, forming a monetary union will not be simple. The launch of a common currency will require a great deal of preparation, and there are fears that it will be a logistical nightmare. It involves lowering and equalising the deficit percentage of the various GCC budgets, standardising interest rate levels and coordinating taxation policies.&nbsp;<br>
                    "Reaching a unified single currency for the GCC states and implementing a practical programme requires a long time and intensive work," former GCC Secretary General Jameel Al Hujailan admitted in a statement.<br>
                    His Excellency Hamoor Sangour Al Zadjali, Executive President of the Central Bank of Oman, says the timetable for a GCC monetary union was discussed at the GCC Summit held in Muscat last December, and historic economic agreements were signed to facilitate unified customs tariffs by 2003 and a single market and currency by 2010.<br>
                    "The GCC Supreme Council directed the Financial and Economic Committee and the Governor's Committee to agree on the economic performance criteria needed to ensure monetary and financial stability and success of the monetary union by the end of 2005 at the latest," he says.<br>
                    Khalid Abdullah, an analyst at Bahrain-Kuwait Bank, says a single currency should be the crowning of an integration process between member states. To create a single currency, Abdullah says Gulf countries must give priority to coordinating policies on privatisation, foreign investment and budgetary deficits.<br>
                    There is also debate about whether the new currency should be pegged to the US dollar or a basket of world currencies.&nbsp;<br>
                    Though the GCC leaders have agreed that the unified currency will be linked to the US dollar, economist Abdullah al-Mojjel in Manama thinks it would be better if the new Gulf currency were pegged to a basket of currencies.<br>
                    As more than 30 per cent of Gulf imports are from the European Union, adding the euro to the currency peg would create more stability, he says.<br>
                    However, Ahmad al-Yusha'a, an analyst in Bahrain, says GCC members should "provisionally link their national currencies to that of the country whose economy is the most powerful, in this case Saudi Arabia".&nbsp;<br>
                    He argues that the Saudi rial should be pegged to a basket of the world's leading currencies and move with market conditions. "But this currency should be supported by all the central banks in the GCC, whose own currencies will be linked to both its highs and lows," he says. By moving with market fluctuations, al-Yusha'a says the price of a currency "will defend itself better despite the risks of collapse for economies based largely on one product (oil)".&nbsp;<br>
                    "While removing their currencies from the dollar in favour of a floating exchange rate, these monarchies must forge ahead in diversifying their sources of revenues."<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>IS IT WORTH IT?&nbsp;</b></font><font face="Verdana" size="2"><br>
                    Some economists question the need for a GCC common currency. They say robust regional trade and volatile currency fluctuations among trading partners are common reasons for countries to join a currency union - neither of which exists in the GCC.&nbsp;<br>
                    Gulf countries tend to produce similar goods and any increase in intra-regional trade primarily reflects re-exports from Dubai.&nbsp;<br>
                    None of the GCC currencies, except the Saudi rial, float on the open market. The majority of the central banks - Bahrain, Oman, Qatar, Saudi Arabia and the UAE - peg their respective currencies directly to the US dollar, and the exchange rates remain within in a tiny band around their pegs.&nbsp;<br>
                    The value of the Omani rial, for example, is fixed at US$2.60. Kuwait is the only exception in the GCC, with the value of the dinar linked to a basket of currencies. Economists say a common currency is therefore unlikely to promote intra-regional trade and investment beyond levels that would otherwise have occurred.&nbsp;<br>
                    Another potential benefit of monetary union - greater international exchange rate stability - would also be of little relevance, they say. A common GCC currency would remain subject to oil price fluctuations and carry insufficient international weight against the US dollar, the euro or the yen. Thus, some argue that while a single currency is economically achievable, its benefits to the GCC would be minimal.&nbsp;<br>
                    "In many respects the Gulf already has one currency - the US dollar," says a senior banking industry executive. "Saudi, Bahrain, Qatar, and UAE currencies have been fixed against the USD for over 20 years, Oman has been fixed for more than 10, and although the Kuwaiti dinar is basket-based, the bulk of the basket is also USD, so it remains fairly steady against the USD. The reason of course for this is that the principal revenue stream for GCC countries is oil, which is traded in USD. If your income is denominated in USD it makes sense to have your local currencies fixed against the dollar.<br>
                    "This means, however, that interest rates in GCC countries are not fixed by local central banks," he continues. "In reality, they are fixed by the US Federal Reserve, as GCC interest rates must remain close to US rates with the currencies fixed.<br>
                    "Therefore, it can be argued there already effectively is one currency. Business in the Gulf accepts GCC currencies as easily as USD. Therefore, there would not be the benefits to business seen, for example, in Europe, where the euro took away volatility between the various currencies that now constitute the euro."<br>
                    He says there are also some negatives in a Gulf monetary union. "One currency for the GCC could produce a slight increase in inflation in places like Bahrain, Kuwait, and Oman. Whilst the purchasing power would doubtless be the same initially, there is a tendency to round up in bigger numbers and businesses would probably use the reset as an excuse to round up prices (similar to what happened in Europe when the euro started as cash)."<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>POLITICAL HURDLES</b></font><font face="Verdana" size="2"><br>
                    Even if the economic benefits were judged worthwhile, the single currency project faces substantial political hurdles. Although the convergence criteria have yet to be established, countries will not want to give up control of fiscal policy, especially as some governments use fiscal means to boost growth and reduce risk in times of low oil prices. Regimes will be understandably unhappy with the associated loss of sovereignty.<br>
                    One currency for the GCC would also mean one central bank to issue it, and some analysts point out that its location could be an issue, just as the location of the European<br>
                    Central Bank was. As for the single currency's name and appearance, it is still early days but this could also be a tricky issue for the GCC states to agree on.<br>
                    "Frankly, I am sceptical of the hyperbole surrounding a GCC common currency," says Beshr Burhan Bakheet, Managing Director at Bakheet Financial Advisors in Saudi Arabia. "It is a political PR exercise which is extremely unlikely to materialise. Yes, oil is a common factor in all GCC economies, but the similarities end there. All the six states pursue their own economic policies, which are often tangential to one another. How can the economy of Dubai be said to be similar to that of Saudi Arabia, or of Oman and Kuwait for instance? Unless there is an effective economic integration, which is an extremely arduous task, unification of currencies will not take off."<br>
                    Integration will require a tremendous leap of faith and such a union has to be politically motivated. But open and free markets, commercial accountability and unified customs tariffs are probably mandatory if the GCC is ever to achieve some measure of its potential for influence globally.&nbsp;<br>
                    If that level of economic integration can be achieved, the jump to a common currency will not be so daunting. And if the global trend towards the formation of currency blocs continues, launching a GCC common currency may well prove to be a smart move.</font></p>

