                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Reluctant Hero</font><p><font face="Verdana" size="1"><b>Dr P. Mohammed Ali is a reluctant celebrity. The recipient of Oman’s Civil Order Grade Three and the Managing Director of Galfar Engineering and Contracting LLC would much rather have his work speak for itself – despite his numerous awards and accolades, he still thinks of himself as a manager simply doing his duty. Shilpa Mathai reports&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    <img border="0" src="../../images/2002_march/40N.jpg" align="left" hspace="5" vspace="5"></font><font face="Verdana" size="2">Mohammed Ali breathes this humility into everything he does; a self-effacing man distinctly uncomfortable in the public eye. But when he was awarded the prestigious Oman Civil Order Grade Three on January 8 this year in appreciation for his long service to the Sultanate, Ali was thrust into the limelight.&nbsp;<br>
                    After much persuasion, OER pinned Ali down for an exclusive conversation to get a glimpse of the man behind Galfar’s success.&nbsp;<br>
                    He was characteristically modest about his accomplishments and confessed candidly: “That His Majesty Sultan Qaboos considers me worthy enough for recognition is a truly humbling experience. Winning the award was a momentous occasion; I hope I can rise to the expectations the honour commands. I feel an increased sense of responsibility and a commitment to set new targets and scale new horizons.”&nbsp;<br>
                    Scaling dizzying heights is almost a way of life for Ali.<br>
                    Oman, 1972: a small, bustling economy grappling with its nation-building process. A small construction outfit called Galfar Constructions is established with a handful of employees. Among them is a bright 23-year-old, a hard worker who enthusiastically participates in all aspects of the business. Though made a manager at such as young age, he works just as hard as any other employee in the scorching sun.&nbsp;<br>
                    Oman, 2002: Galfar Constructions LLC is Oman’s largest construction company with 14,000 employees and sprawling office blocks in the Ghala Industrial Estate. From a civil engineering company, it has diversified into the mechanical, electro mechanical, and heavy civil engineering industry, with offices in Brunei Darussalam, Qatar and Abu Dhabi… the only company in Oman capable of handling all construction requirements under one roof.&nbsp;<br>
                    The 23-year-old manager has grown with his company – today he is a stately 53-year-old, much feted and toasted in Muscat and one of the best known Indians in the Gulf.&nbsp;<br>
                    But he still works just as hard with his team – Mohammed Ali is a team player who categorically maintains that none of Galfar’s achievements can be attributed to him alone. “Success in any organisation is never the effort of any one single person,” he says. “From the shareholders to the staff and the management team, everyone plays a part. I merely passed the ball along for my team to score.&nbsp;<br>
                    “We have always worked as a team from day one. Some of our earliest ventures were in the Interiors. No other company was willing to work there, the risks were high but so were the rewards. We have worked in some of the toughest places; it was an extremely hard life. However, we survived and built up an infrastructure to cope with challenges. We have come a long way since then but our motto essentially remains unchanged – we don’t shy away from a challenge. We take it one step at a time; we set achievable targets and do things differently.”&nbsp;<br>
                    Galfar has been involved with some of the biggest construction projects in the Sultanate, from the Salalah Waste Water Project to hospitals in Sur, Nizwa, Ibri, the Nizwa Souq, stadiums in Ibri and Sur, and the new link road in Qurum.&nbsp;<br>
                    Ali says Galfar is a construction company that seeks to build a brand. It believes in delivering quality products stamped with its hallmark of reliability.<br>
                    “As a result people trust us. They know what we stand for, that when we undertake a job we will deliver a guaranteed product within the stipulated time frame. Every project of ours represents our brand and its unique set of attributes.”<br>
                    This drive to do something to the best of one’s abilities is something, he says, that has carried him through his career.&nbsp;<br>
                    “I always try to do my best. I started my career at the age of 17; I was working in India in Mizoram when my father made arrangements for me to take up an assignment in Dubai. The Gulf in those days was the expatriate job seekers El Dorado, and I was a young hopeful with stars in my eyes. It was while working in Dubai I met Sheikh Salem, the Chairman of Galfar, who had come down to source manpower, materials and equipment to start a construction outfit. I was interviewed along with five others – we were all recruited and that was the beginning of the company.”<br>
                    Ali claims to be a cautious risk taker. “I do take risks but I do not jump into the fray without doing my homework. I understand what’s required and then work a way out. I discuss the issue with relevant people and work alongside them.&nbsp;<br>
                    “I believe,” he continues, “in leading by example. So when we venture into unknown territory, I go there first – that encourages and strengthens my team. You have to be there for your people; even now that’s my number one priority. Whenever and wherever my people need me I will be, physically, there for them, no matter what. We have to make people believe in themselves and in their capabilities in order to motivate them. You need your people to trust you; when they believe in you and in themselves, the company moves forward.”&nbsp;<br>
                    This spirit propelled Galfar to rapid growth; subsidiaries spun out of the parent company and today the group consists of 14 companies.&nbsp;<br>
                    “Every new project we undertake, we try to make it a success. Whatever you do, do it well – if you have that belief and implement it conscientiously, then you will excel. You set standards for yourself which in turn will improve the business.”<br>
                    That Ali walks the talk is evident; his employees narrate snippets of his simplicity and loyalty to the institution. In a bid to “give something back to the community that nurtured him”, he has taken on the mantle of an educationalist. The Caledonian College of Engineering, The Indian School in Al Ghubra and the Oman Medical College are all promoted by Galfar.&nbsp;<br>
                    So is Ali a philanthropic? “Not at all,” he denies vehemently. “I’m not a saint, but raking in money does not goad me anymore. I won’t deny it was enjoyable when I could afford to meet my first few needs, but even then I always focused on the task entrusted to me, that of managing the company. Money then becomes a by-product of that success.”&nbsp;<br>
                    Ali says education and development of human resources is vital for any country.&nbsp;<br>
                    “However rich the country may be in terms of its GDP and economic indicators, if its citizens are not educated and competent enough to handle its wealth and resources, what is the use of all its money? Education is the key to developing people and the community.”&nbsp;<br>
                    It was this vision that led to the formation of the Oman Medical College, the Sultanate’s first private sector-driven medical college. “There was a need for professional colleges, and we felt obligated to contribute our might by deploying resources and expertise to help develop Omani talent. Once there is a model, others are encouraged to follow; now two or three other colleges are coming up, so we feel proud to have led the way.&nbsp;<br>
                    “What else can you do to repay your debt of gratitude to an otherwise wealthy nation?” His ties to the Sultanate obviously run deep. “You do whatever you can to help and lead by example, hoping that others will follow on the path you have charted out,” he says.<br>
                    “It’s difficult to be a first mover, but if you can do that and remove the obstacles involved, that will help others who come in later have a smoother ride. Everything is difficult unless someone makes it easy. Knowing that I did something useful gives me tremendous satisfaction.”&nbsp;<br>
                    Ali is just as passionate about his family as he is about his work; his three daughters are away studying abroad, his only son is still in school in Muscat. Success hasn’t changed him, because he has not forgotten his roots. “I enjoy going back to my village. In fact though I live here, I see my relatives very often.”&nbsp;<br>
                    But not content to rest on his past successes, Ali says he has still has much to achieve.&nbsp;<br>
                    “There are still many more miles to go,” he muses. “I would like Galfar to become a GCC powerhouse and become the number one construction company in the Middle East. I am also keen that we maintain whatever we have achieved so far. Once that sustainability is assured, we can aim for the next rung.&nbsp;<br>
                    “But I don’t want to proclaim anything,” he quickly adds. “I would much rather have our work speak for itself.”&nbsp;<br>
                    And then he smiles, the cue that work beckons and he has to go.&nbsp;</font></p>

