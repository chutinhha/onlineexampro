            is India’s closest neighbour in the Gulf and deep-rooted bonds of 
            friendship and strong cultural affinities characterise our 
            historical relationship going back to the days of the Indus Valley 
            Civilisation over five millennia ago. <br>
            <br>
            The past few centuries have witnessed constant people-to-people and 
            commercial exchanges between India and Oman spurred by the presence 
            of a merchant community from India in Oman. As His Majesty Sultan 
            Qaboos bin Said Al Said ushered in his vision of an Omani 
            Renaissance, the age-old ties paved the way for bilateral 
            interaction progressing into a multi-faceted contemporary 
            relationship with the people from India working side by side with 
            Omanis in their quest for economic growth and development.<br>
            <br>
            A Treaty of Friendship, Commerce and Navigation was signed between 
            India and Oman in 1953, one of the first such agreements signed by 
            India with any country in the Gulf. The Indian Consulate opened in 
            Muscat in 1955, also one of the first diplomatic representations of 
            India in the region. It is, indeed, a matter of much pride for us to 
            celebrate 50 years of diplomatic ties with Oman this year. Our joy 
            on this happy occasion is further heightened by the fact that this 
            year also marks 35 years of His Majesty’s accession. <br>
            <br>
            In recent years, a number of high level visits have helped foster 
            closer understanding and the development of a substantial 
            partnership between India and Oman. Recently, India’s Minister of 
            External Affairs K. Natwar Singh visited Oman to further boost 
            bilateral ties. The visit also saw the signing of an Extradition 
            Treaty and an MoU between the State Audit Institution of Oman and 
            the Comptroller and Auditor General of India, for cooperation in 
            audit matters.<br>
            <br>
            Agreements on Avoidance of Double Taxation and Promotion and 
            Protection of Bilateral Investments between the two countries 
            provide the requisite legal framework for facilitating trade and 
            investment flows. An India-Oman Joint Commission and a Joint 
            Business Council provide institutional framework for planning, 
            monitoring and facilitation of business relations and economic 
            cooperation. <br>
            <br>
            Oman will be the host of the 2nd India-GCC Industrial Conference in 
            Muscat in January 2006, a major India-GCC event, which will bring 
            together the most important economic stakeholders in both regions 
            together for further strengthening India-Oman and India-GCC ties.<br>
            <br>
            It is estimated that at any given time, there are more than 1,500 
            Omanis studying in India or undergoing training and around 12,000 
            Omanis visit India every year. India and Oman are founding members 
            of the Indian Ocean Rim Association for Regional Cooperation. <br>
            <br>
            Many Indian companies have executed prestigious projects over the 
            years in Oman with several JVs in existence including in IT and IT 
            education and training. Indian banks and financial institutions also 
            operate in Oman. Omani companies also have joint ventures in India 
            in banking, biotechnology, IT and many other fields.<br>
            <br>
            India-Oman trade has been growing and India is the one of the 
            largest sources for imports into Oman including textiles and 
            garments, machinery and equipment, electrical and electronic items, 
            chemicals, iron and steel products in addition to traditional items 
            like tea, coffee, spices, rice and meat products. Omani exports to 
            India include copper, metal scarp and dates. <br>
            <br>
            The 350,000-strong Indian community, including professionals and 
            managers occupying senior positions in corporate Oman, have proudly 
            associated themselves with the economic growth and modernisation of 
            Oman and acted as a dynamic link binding the two countries with a 
            seminal contribution in the fields of commerce, healthcare, 
            education, horticulture, finance, construction and communication.<br>
            <br>
            With the leadership in both the countries sharing a vision for an 
            even more substantial partnership, India-Oman relations are 
            definitely set for a major upswing in the coming years. The 
            $969-million Oman-India Fertiliser project, one of India’s largest 
            joint ventures abroad which will also contribute significantly to 
            Oman’s endeavours to diversify its economy is, perhaps, the best 
            manifestation of the desire for a strong India-Oman partnership. It 
            goes on steam later this year, a most positive augury in the year 
            when we mark the Golden Jubilee of our diplomatic ties with Oman.<br>
