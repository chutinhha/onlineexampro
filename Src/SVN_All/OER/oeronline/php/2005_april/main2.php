<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">US 
comes a calling</font><font face="Verdana" size="1"><br>
<br>
<b>Vicky Kapur gets Face2Face with Catherine A. Novelli, Assistant US Trade 
Representative for Europe and the Mediterranean, who was in Oman as the lead 
negotiator for the first round of Oman-US Free Trade Agreement negotiations. 
Excerpts from a free-flowing discussion</b><br>
<br>
<b><img border="0" src="us_comes.jpg" align="left" width="250" height="169">Welcome to Oman, Catherine. How are the Oman-US FTA negotiations progressing?</b><br>
Thank you. About fourteen different groups (of negotiators) are meeting 
simultaneously so that we can work most efficiently. We are here for the first 
round of negotiations, and what we are looking for is a comprehensive agreement 
that covers all sectors of economic activity – from trading goods to trading 
services, investment, intellectual property, government procurement, Customs, 
telecommunications… the list goes on. The idea is to build a framework for a 
relationship that will endure into the future. <br>
<b><br>
Why are you looking at a comprehensive agreement as opposed to individual 
sector-wise arrangements?</b><br>
We believe it’s most conducive to have a comprehensive agreement going as 
opposed to a piecemeal approach as there are so many interconnections between 
different things – services are connected to investments, imports/ exports of 
goods etc. If you do one piece at a time, you do not get the full benefit – 
therefore, we take the comprehensive approach. We have found that this approach 
has been very successful in increasing trade and investment between our country 
and our partners. <br>
<br>
We’re taking all the things in the bilateral investment treaty and rolling them 
into one comprehensive agreement. Earlier, for quite a long time, the United 
States wasn’t really negotiating very many free trade agreements and therefore 
we took investment treaties separately and bilateral trade treaties separately. 
However, now that we are once again negotiating FTAs, it makes sense to put it 
all together. We still sometimes do bilateral investment treaties with countries 
where we aren’t necessarily pursuing a comprehensive trade relationship. <br>
<br>
By and large, in the Middle East, we have comprehensive FTAs with different 
countries. In fact, even with countries where we had just investment agreements 
earlier, like Morocco, we have signed FTAs that include the investment issues 
and supersede the old bilateral arrangement. There are still some treaties like 
the double-taxation treaties that are separate because they are very specialised, 
but FTAs are basically meant to be as comprehensive as we can make them. <br>
<br>
The idea then is to take these individual agreements we are negotiating with 
individual countries and meld them into a comprehensive Middle East-US FTA. Two 
years ago, (US) President George Bush set a goal for 2013 to complete that 
target, and we’re moving towards that goal. <br>
<br>
It’s an ambitious target, but when this target was set, we had an FTA with 
Jordan, a very old FTA with Israel, from 1985, and an almost defunct Trade &amp; 
Investment Framework Agreement (TIFA) with Egypt – that was about it. <br>
<br>
In less than two years’ time, we concluded two very good FTAs, began 
negotiations with two more (Oman and the UAE), we have TIFAs with Qatar, Kuwait, 
Saudi Arabia, Yemen, Algeria, Tunisia, and Egypt. We have done an awful lot in a 
short amount of time. I also think that the momentum of economic reform has 
really picked up in this region. The speed of change here is increasing – not 
just here in Oman, but regionally. So, that makes it easier to contemplate 
actually being able to complete something like this by 2013.<br>
<br>
<b>What stops you from negotiating a comprehensive FTA with the whole of Middle 
East instead of approaching individual countries?</b><br>
We didn’t put everybody into one big basket together because not everybody is in 
the same place (in terms of economic openness). Oman is very open, but maybe 
some other countries are still struggling to be part of the WTO – maybe they are 
not there yet. The idea is to work with these countries individually rather than 
assuming one-size-fits-all. We would first help them advance up the ladder of 
economic openness, and then when we mutually agree that it makes sense, we would 
move towards negotiating an FTA. <br>
<br>
There is an interim step—TIFA—that we have signed with a number of countries in 
this region. The TIFA provides a framework for the process, which is really a 
dialogue, to learn more about our partner and for them to learn about how we do 
things, and to talk about what our goals would be if we did negotiate an FTA 
with them and what kind of things should be covered so that by the time you get 
to the negotiating table, everybody would be on the same page. We also offer to 
solve any concrete business problems that might exist. We have signed TIFAs with 
Algeria, Tunisia, Egypt, all GCC countries, and with Yemen – we’re moving 
rapidly to try to engage them. <br>
<br>
<b>The six countries comprising the GCC have a precise trading system in place. 
Why not approach the GCC as a bloc to negotiate an FTA?</b><br>
We totally support the GCC and we think it is a good thing and we think that the 
(economic) openness among the countries is good and that is part of the reason 
why president Bush put forward the idea of the Middle East Free Trade Area 
because we think that it’s very important that there be liberalisation among the 
region and growth that way and there be trade among all the countries here in 
this region there isn’t much intra-regional trade in this area it is in other 
regions. <br>
<br>
<img border="0" src="us_comes1.jpg" align="left" width="150" height="218">But the reason why we didn’t engage the GCC as a whole is the same reason why we 
didn’t engage the Middle East as a whole – not every country in the GCC is at 
exactly the same stage of making its economic decisions about how open it is 
going to be, how it’s going to change its economy etc. In fact, one of the 
countries in the GCC isn’t in the WTO yet. The agreements that we do are based 
on a floor of WTO commitments and then they go above that floor. So, you really 
need countries to be at that floor because otherwise, you have no foundation of 
building your agreement. <br>
<br>
So, in looking to the fact that countries are at very different places, if we 
were going to choose to take the GCC approach, then we would have had to be 
waiting for many years before everybody was at the same stage, and we didn’t 
think that that was right. We wanted to engage people right away and if certain 
countries were ready to move forward with a more liberal approach, then we 
thought that that was good. The countries with whom we have negotiated or are 
negotiating the FTA and most of the ones with whom we have the TIFA have 
confirmed to us that there is nothing in the GCC rules that is a problem in 
terms of taking this approach.<br>
<br>
<b>One of the criticisms of this approach is that GCC as a bloc would have had 
enhanced negotiating power with the US as against individual countries…</b><br>
Our idea of the FTA is that it is underneath the vision that individual leaders 
have for their own countries. It’s almost a subset of that in that it fits 
nicely into that vision. In a certain way, we do not see it as needing to have 
negotiating leverage per say because the point is to lock in the open policies 
and decisions that have already been made. <br>
<br>
That’s what we found particularly in the Gulf – Bahrain, Oman, and the Emirates… 
you have very open (economic) traditions. So, there isn’t a real conflict 
between what’s in the FTA and what choices you’ve already made. Of course, our 
economy is very very open already, and we’re guaranteeing that we’ll keep it 
that way and also special privileges in terms of Zero Tariffs and access to our 
government procurement market. <br>
<br>
<b>What benefits is the FTA expected to bring about?</b><br>
The degree of the FTA’s impact may vary for different regions and countries, but 
the point is that the agreement will give Oman access to the largest market in 
the world – both for goods as well as services. On the other hand, it puts Oman 
on the map as a country that is committed to having open trade and investment 
policies, and that makes it very attractive for foreign investors not just from 
the US, but other countries as well.<br>
<br>
For instance, in terms of our agreement that has been in place for a long time 
in this part of the world is the Jordan FTA. When we started the negotiations in 
2000 [Catherine was the lead negotiator for the US-Jordan FTA as well], they 
exported $11 million (RO4.23 million) worth of goods to us – which is very 
small. <br>
<br>
I just met with the Jordanian minister for planning a few weeks ago, and 
according to the statistics for 2004, the exports are now up to $1.2 billion 
(RO462 million). <br>
<br>
Their FDI (foreign direct investment) has gone way up too – it was about $600 
million (approximately RO231 million) when we started the negotiations. By 2002, 
it was $2.4 billion (RO923 million), and it has climbed since – so again, a 
dramatic increase. We also see things happening there because of the 
intellectual property protection (IPR). They have developed a very sophisticated 
pharmaceuticals industry – one of the largest exporters now in the Middle East. 
That industry’s exports to the world grew 30 per cent last year, which is again 
very dramatic. <br>
<br>
Because of the IPR in place, US pharmaceuticals companies decided to make Jordan 
their clinical research and trial base, as they felt comfortable about their new 
products not being pirated or plagiarised. That’s given all kinds of 
opportunities to Jordanian doctors and chemists and other researchers to really 
be able to engage in such activities, which has in turn created a number of job 
opportunities for the locals there. <br>
According to the Jordanian government, all this economic activity has resulted 
in 40,000 jobs created in the country—just because of the FTA—and that’s pretty 
significant in such a small population. <br>
<br>
<b>Which particular sectors of the Omani economy do you believe will benefit the 
most?</b><br>
It’s hard for me to exactly predict that because one thing that I have learnt 
over the years is that the private sector will always surprise you. Who would 
have ever even thought the Internet being what it is today, ten years ago? One 
of the interesting things that happened in Jordan—and no one expected this to 
happen—is that as exports surged dramatically, a new service industry started 
springing up to facilitate the exports. So, it’s clear that the private sector 
identifies the opportunities and we do not know from out vantage point what 
opportunities will it tap or create. <br>
<br>
Oman has incredible natural resources in terms of oil, and so you would expect 
that sector to develop. But then, as that sector develops further, it may 
require different kinds of services. It may require more, say, accounting 
services or consulting services or marketing – things not necessarily exactly 
about extracting oil. <br>
<br>
And one thing that I would say, and that’s my personal view, is that having had 
some chance to come and see the country a little bit, clearly this is a place 
that is just right for tourism. I’ll tell everyone I meet in the US that they 
have to come to Oman because it really is unique.<br>
<br>
<b>Which sectors of the US economy will benefit the most from an FTA with Oman?</b><br>
Again, I really can’t say for sure, but if, say, Oman develops tourism, that’s 
an area where we have great expertise and I am sure we’d like to be part of that 
development. But it really depends on what opportunities present themselves. It 
would be hard for me to say, because whatever I say, I’m bound to be wrong in 
whatever I say, or at least incomplete, anyway.<br>
<br>
<b>What changes would you want Oman to make in its regulatory or administrative 
environment in order for the FTA to go through?</b><br>
Right now, the teams are in very detailed discussions, and there may be certain 
things that are different and that need to change – both in Oman as well as in 
the US. Just so you know, we may have to change our laws in order to implement 
the FTA. <br>
<br>
Our laws offer a certain level of protection (to certain sectors of the economy) 
and we are offering to drop that protection for Oman. We’ll have to change our 
tariff laws and those governing government procurement and certain safeguards. 
We changed these and other laws when we signed the FTA with Bahrain – we’ll have 
to change them for Oman as well. So, there may be certain things in Oman that 
need to change – but that will be for the Omani team to decide as we talk about 
what kind of obligations make sense between our two countries. <br>
<br>
However, we have not suggested any reform agenda to Oman – or, for that matter, 
to any of our partners. This is Oman’s decision, and Oman has undertaken its own 
agenda for reforms or change. We learned about that in the TIFA, and after 
learning about it, we realised that it was a mutual fit for us and the FTA 
negotiations are the logical next step. <br>
<br>
It wasn’t us dictating anything because what we look at is that any kind of 
change or reform needs to be a decision of that country’s and it needs to be 
done because the people in that country think it’s in their own interest to do 
it. It’s not really up to us to telling people what to do.<br>
<br>
<b>How long do you think the negotiations with Oman will take?</b><br>
I can’t put a precise date, but I can tell you that we are ready to move as 
quickly as the Omani side is, and they have indicated a real interest in moving 
quickly. We expect it to take months, and not years. Part of the reason why we 
can do this is because we have spent almost a year talking between ourselves 
about the various obligations and identifying the gaps between what we do and 
what Oman does and so there’s been a great deal of groundwork done before the 
actual negotiations began. And that’s why we can move this quickly – it isn’t 
because people aren’t considering carefully what they are doing. It’s just that 
they have already been doing that for a year. <br>
<br>
Certainly, I think that the negotiations will be concluded and the agreement 
signed before the end of this year, but as far as it coming into force is 
concerned, I hope—and this depends on the US Congressional schedule—it will be 
sometime next year.<br>
<br>
<b>What about conflict resolution?</b><br>
In the case where we have signed an FTA, we have a whole system of trying 
through conciliation and discussion to solve a problem. We don’t immediately go 
in for litigation. However, in case we reach the litigation stage—so far, none 
of our FTA partners have ever reached that stage—then the partner can choose 
where to litigate – i.e., under the WTO regime or under a bilateral court. </font>
