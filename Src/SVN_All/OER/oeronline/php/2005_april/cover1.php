            </font><b><font face="Verdana" style="font-size: 8pt">
            Lessons In Choosing Your No. 2</font></b></p>
            <p align="justify" dir="ltr"><b>
            <font face="Verdana" size="3">I</font></b><font face="Verdana" size="1">t’s 
            no small task to find someone who is content to complement you now – 
            with the skills to take your job later. If it’s true that you’re 
            only as good as the team you’ve assembled around you, then few 
            decisions could be as important as who will be your No. 2. Just ask 
            Douglas Daft, whose lack of a capable successor sullied his 
            departure from Coca-Cola. In fact, The Coca-Cola Co. has yet to 
            recover from the death of its revered CEO, Roberto Goizueta, who 
            lost a brief battle with lung cancer in 1996. The market was 
            forgiving at the time because Goizueta had long been grooming Doug 
            Ivester to take over. <br>
            <br>
            But Ivester failed to live up to expectations and was gone after two 
            tumultuous years. The following CEO, Douglas Daft, continued to 
            struggle with layoffs, management turnover, and a slumping stock 
            price until he too decided to call it a day. In fact, it had been 
            obvious for some time that the company’s management team is in 
            disarray. Yet, when Daft announced his intention to retire as early 
            as February 2004, the board was unprepared. It had no succession 
            plan, and Daft had to drag as chairman and CEO till May 31, 2004, 
            when the company’s board installed the 61-year-old Neville Isdell, 
            who took charge on June 1, 2004. <br>
            <br>
            James Cantalupo, on the other hand, thrived as CEO of a resurgent 
            McDonald’s in part because he had highly capable Charlie Bell at his 
            right hand. That arrangement proved doubly fortunate for the company 
            after Cantalupo died of a heart attack in April. But finding a 
            capable No. 2 who can effectively carry out the mission of the CEO 
            is far from being a simple equation. Worthy successors don’t 
            necessarily make the best lieutenants. And competitive issues can 
            arise for presidents or chief operating officers eager to get their 
            shot at running the show.<br>
            <br>
            CEOs must be able to manage those tensions well. Boards and outgoing 
            CEOs can be all over the map in terms of their criteria for an heir, 
            including what comprises the package of crucial skills and whether 
            the current CEO should be a template for—or a negative image of—the 
            next. Familiarity with the current CEO has become less important 
            than the candidate’s personal ethics, as companies seek to inoculate 
            themselves from scandal. That may be one reason companies are more 
            often going outside their own ranks for successors.<br>
            <br>
            Ideally, a CEO’s No. 2 is both a valuable right hand in running the 
            company today as well as someone who has the clear capabilities to 
            ascend to the CEO chair later on. Cantalupo, for instance, had 
            promoted Bell to president and COO in January 2003; so, Bell didn’t 
            skip a beat in continuing McDonald’s sales turnaround. Similarly, 
            Kevin Rollins served for several years as president and COO of Dell 
            Computer before Michael Dell handed him co-CEO duties and finally 
            full CEO status earlier this year. One trick often is helping an 
            heir apparent make the crucial transition from an effective 
            complement to the boss to being a leader with the perspective and 
            stature to take the helm. Selecting an heir apparent who can kick 
            into that visionary role in a heartbeat is perhaps the most 
            important criterion. <br>
            <br>
            At the same time, the No. 2 must be content with a supporting role, 
            at least temporarily. A good number of people who are supposedly in 
            line for the top role will only wait so long. It becomes a bit of a 
            chess game. The CEO needs to manage the career of their 
            second-in-command as well as their emotions. Gradually parcelling 
            out more duties to an heir apparent and grading his or her progress 
            is one of the best ways CEOs can ensure both that they’ve made a 
            wise choice and that the transition will be smooth. <br>
            <br>
            Building bench strength beyond one candidate is another important 
            factor. It can prevent the sort of catastrophic breakdown in the 
            succession process that Coca-Cola experienced. In 2001, in a rare 
            grab outside the vast executive ranks of the company, Daft reached 
            into Time Warner to pluck Steven Heyer, who was president and COO of 
            the Turner Broadcasting unit. He promoted Heyer to Coke’s president 
            and CEO in late 2002. But when the company’s board forced out Daft 
            early last year, they also decided to snub his heir apparent. 
            Coca-Cola had no other reasonable internal option so directors went 
            courting the likes of Jim Kilts, Gillette’s CEO. Coke’s stock price, 
            prestige and prospects plummeted until finally, in May, they lured 
            61-year-old bottling-company alumnus Neville Isdell out of 
            retirement for the top job.<br>
            <br>
            It’s perfectly okay to look across your ranks at the time of 
            succession and say, ‘It’s not here.’ That doesn’t mean these 
            executives aren’t all very effective in their current roles, today. 
            It just means that the next CEO isn’t here. In Good to Great, author 
            Jim Collins notes that most stellar successions involve internal 
            candidates. But boards in general have been behaving more 
            conservatively lately, favouring sitting or former CEOs over groomed 
            heirs. The smaller the company, the greater the likelihood that a 
            CEO will want someone directly beneath him who can both function as 
            a capable No. 2 and be in line to succeed. <br>
            <br>
            Experts differ on how much to disclose about succession plans. Some 
            recommend candidness, the logic being that if the CEO is openly and 
            consciously making the decision, it mitigates conflict beneath him 
            and helps the whole process of planning for it. Others forswear the 
            early naming of a successor, the rationale being that once you 
            identify someone as heir apparent, it puts undue pressure on the 
            anointed one and discourages those who haven’t been anointed.<br>
            <br>
            In the end, there is no one-size-fits-all approach for picking a No. 
            2. Like so many other issues facing CEOs, it hinges on a leader’s 
            instincts about people and on an astute sense of timing. The only 
            certainty is that it’s best for everyone if a CEO faces up to the 
            challenge, rather than allowing uncertainty to fester and fate to 
            lead the way.</font></p>
