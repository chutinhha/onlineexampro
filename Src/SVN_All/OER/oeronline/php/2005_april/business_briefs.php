                    <b>TRA to host Interconnection &amp; Price Regulation Workshop</b><br>
                    Under the auspices of His Excellency Sheikh Saud bin 
                    Sulaiman Al Nabhani, the undersecretary of Transport and 
                    Communications, the Telecommunications Regulatory Authority 
                    (TRA) will host the Arab Regional workshop on 
                    Interconnection and Price Regulation during the period from 
                    April 3-6, 2005 at the Grand Hyatt in Muscat.<br>
                    <br>
                    Hosting this workshop comes in line with TRA’s recognition 
                    of the importance of this type of workshop, which 
                    contributes in human capacity building for specialists in 
                    the region, and also in line with TRA’s targets of 
                    regulating the telecommunications sector in the Sultanate. 
                    The workshop aims at highlighting the basic issues of 
                    interconnection and price regulation and their significance 
                    for all concerned parties including regulators, operators 
                    and end-users. The objective of the workshop focuses on 
                    providing a deeper understanding of the interconnection 
                    management which requires a highly trained cadre both 
                    technically and administratively keeping abreast with the 
                    daily and long-term regulatory requirements. <br>
                    <br>
                    Presentations will cover technical, economic and legal 
                    aspects of interconnection as well as economic aspects of 
                    price regulation. It will also include cases on best 
                    regional and international practices interconnection. The 
                    events of the workshop will commence on the first day with 
                    the opening ceremony under the auspices of His Excellency 
                    the Undersecretary, which will be followed by the activities 
                    of the workshop continuing for four days. The presentations 
                    given by participants will include the following topics:<br>
                    <br>
&nbsp;• Costing issues of interconnection<br>
&nbsp;• Technical aspects of interconnection<br>
&nbsp;• Issues of interconnection and the World Trade Organisation<br>
&nbsp;• Case studies of interconnection in different countries<br>
&nbsp;• Practical exercises on dispute resolution and billing conflicts<br>
&nbsp;• Economic aspects of price regulation<br>
                    <br>
                    The workshop is organized under the supervision of the Arab 
                    Center of Excellence managed by the International 
                    Telecommunications Union (UTI) and in cooperation with 
                    Intercai which is a European Telecom consultant specializing 
                    in regulatory policies. The Arab Center of Excellence plays 
                    a vital role in developing the telecommunications sector in 
                    the Arab region which is reflected in providing assistance 
                    through development of expertise in the field of policy, 
                    regulation, management and technical capabilities. Oman, 
                    represented by TRA, has been honored by being selected to 
                    host the Arab Center of Excellence for the coming three 
                    years. <br>
                    <br>
                    This workshop targets specialists in interconnection and 
                    price regulation from administrations, regulators, operators 
                    and service providers in the telecommunications sector. 
                    Sponsorship s received from telecommunications companies 
                    prove their understanding of the importance of this type of 
                    workshop and their willingness to contribute to the 
                    development of telecom sector in Oman. <br>
                    <br>
                    In this context, Oman Mobile is the platinum sponsor and 
                    Ericsson, Huawei and Nawras are the golden sponsors. The 
                    Silver sponsors’ spot was filled by Mustafa Sultan Telecom, 
                    Royal Oman Police and Bank Muscat. Omantel and Infocomm have 
                    provided the workshop venue with wireless LAN for Internet 
                    access.</font></p>

