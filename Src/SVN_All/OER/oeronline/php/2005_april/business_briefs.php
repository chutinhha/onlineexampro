                    <img border="0" src="pdo.jpg" align="left" width="100" height="109">PDO commissions Saih Nihayda gas plant</b><br>
                    Petroleum Development Oman (PDO) has achieved another 
                    significant landmark in its history of supplying natural gas 
                    for the nation’s development with the commissioning of the 
                    $150-million (RO57.69 million) Saih Nihayda plant. The new 
                    plant began to export gas and condensate (a liquid 
                    hydrocarbon by-product of gas production), recently. <br>
                    <br>
                    The Saih Nihayda plant, which is processing gas from the 
                    condensate-rich, deep gas reservoirs of the Saih Nihayda 
                    field, is capable of exporting up to 20 million cubic metres 
                    of gas per day and 10,000 cubic metres of condensate per 
                    day. Its construction was carried out under contract by a 
                    joint venture between the local firm, Al Hassan, and the 
                    Canadian firm SNC Lavalin. It is the largest gas project to 
                    have been completed by PDO since the completion in 1999 of 
                    the upstream LNG project, which consisted of the development 
                    of the Saih Rawl and Barik gas fields as well as the 
                    construction of the central processing plant at Saih Rawl 
                    and the laying of a 48-inch pipeline from there to Qalhat, 
                    near Sur. <br>
                    <br>
                    The plant, which took just four years from the concept 
                    definition to commissioning, will be needed to feed gas to 
                    the nation's third LNG train, which is currently under 
                    construction at Qalhat. Until the third liquefaction train 
                    is commissioned later this year, the Saih Nihayda plant will 
                    be supplying gas to the two existing liquefaction trains of 
                    the Oman Liquefied Natural Gas plant, also at Qalhat, and to 
                    other costumers via the existing 48-inch pipeline. To ensure 
                    an adequate future supply for all costumers, the capacity of 
                    the 48-inch pipeline is also being expanded by PDO in 
                    another major project that is currently underway.</font></p>
