<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000">It 
takes two to tango!<br>
</font><br>
<font face="Verdana" size="1"><b>It’s a battle for the small chip in your 
mobile, but it is gaining big proportions now. At last, after much wait and 
hype, competition has come of age in Oman’s telecommunications sector, and 
residents of the country can now choose their mobile operator. <br>
<br>
An OER report</b><br>
<br>
<img border="0" src="telcom.jpg" align="right" width="100" height="219"></font><b><font face="Verdana" size="3">C</font></b><font face="Verdana" size="1">all it 
the celebration of competition! With the country’s second mobile operator, 
Nawras Telecom (a consortium formed by Q-Tel together with Danish TDC and Omani 
partners), launching its services on March 16, 2005, the monopoly of the 
State-owned Oman Mobile was shattered. The competition has resulted in new 
services at reduced prices, and there seems to be somewhat of a price war going 
on between the two mobile operators in the country. <br>
<br>
The challenger, Nawras, of course, fired the first salvo when the company during 
its launch, announced lower (than Oman Mobile) subscription and call rates. 
Within three days, the incumbent, the good ol’ Oman Mobile, came calling, and 
the company launched new and improved services at lower rates to counter the 
offers of the new entrant. Bang, a couple of days later, Nawras announced 
further reductions in its prices. Of course we haven’t heard the end of it yet. 
Meanwhile, the customer is trying to figure out the ‘better’ offer. <br>
<br>
<b>Core competition</b><br>
Experience has demonstrated that free and open competition benefits individual 
consumers and societies as a whole by ensuring lower prices, new and better 
products and services, and expanded consumer choice across the globe. Bahrain 
and Kuwait, the first to allow competition in the telecom sector in the GCC, 
have been reaping benefits from the entry of new operators.<br>
<br>
The Bahraini telecom market has witnessed a major change ever since it opened up 
its telecom sector to a second GSM player, MTC-Vodafone in 2000. This not only 
ended the decades-old monopoly of Batelco, but the sector also witnessed 
double-digit growth rates. Similarly, the growth in Kuwait’s GSM space is also a 
clear indication that the market can absorb competition if provided with right 
products at the right prices. Going by the example that its GCC neighbours have 
set, Oman is poised to reap the benefits of competition as the Sultanate’s 
second mobile operator is now connected. <br>
<br>
In the GCC, the Omani telecom market has the least penetration levels. According 
to various research reports, only a minority of the population in Oman enjoys 
the benefits of mobile communication. With a population of nearly 2.5 million, 
the current GSM usage in Oman is much lower than other countries in the region. 
It roughly amounts to a mere 25 per cent of the population – thus, there is 
enormous scope for increasing the overall mobile penetration.<br>
<br>
Projections show that Oman can become a very lucrative market for mobile 
telephony. Firstly, there is a lot of potential because of the untapped market, 
and secondly, competition and, consequently, improved prices and services, are 
bound to increase the usage. Even before the entry of the second operator, a 
major boom in the GSM subscriber base occurred in the year 2001. <br>
<br>
The subscriber base has been growing steadily ever since then and has exceeded 
the fixed line subscriber base. According to the Arab Advisors Group, Oman’s 
cellular penetration rate will exceed 50 per cent in 2008, corresponding to more 
than 1.5 million subscribers.<br>
<br>
<b>First among equals</b><br>
Realising the potential the sector has to offer, the government of Oman 
liberalised the market. Having granted the second licence, the government has 
opened the doors to competition, but the important question remains – which 
operator has the edge? <br>
<br>
Oman Mobile, for one, is well entrenched in the market, and is at a vantage 
point in terms of possessing the infrastructure and huge a subscriber base. 
Congratulating Nawras on the launch of commercial operations, Dr Amer Awadh al 
Rawas, Managing Director of Oman Mobile, brushed off any threat by the new 
operator to Oman Mobile’s market share and revenues. ‘‘Our sales have witnessed 
positive growth despite the launch of Nawras. This confirms that the Telecom 
Regulatory Authority’s (TRA) policy of opening up the market has been a wise 
one,’’ he said during a hurriedly convened press conference to announce Oman 
Mobile’s counter to the challenger’s launch offers.<br>
<br>
Oman Mobile connects almost 95 per cent of the country, and has roaming 
agreements with 167 operators in 77 countries, keeping its users connected. 
Besides, it has the most comprehensive distribution network – 1,080 stations all 
over Oman, with new service centres in densely populated areas like Muscat, Sur 
and Buraimy, of which 13 exchanges are in Muscat itself. <br>
<br>
‘‘The Oman Mobile infrastructure has been in place since November 1996, and it 
has achieved tremendous success with numerous services, including voice mail, 
multimedia etc.,’’ said Dr Rawas. Besides, he disclosed that there were 11,000 
outlets all over the country where Hayyak cards, Oman Mobile’s pre-paid 
offering, could be recharged.<br>
<br>
The company provides nationwide coverage encompassing rural areas as well, in an 
effort to provide communications services to all segments of society, he 
stressed. Besides the points-of-sale for Hayyak top-up vouchers, Oman Mobile 
also operates 400 outlets for Hayyak starter kits, which include convenience 
stores such as Select and Souk, and supermarkets and hypermarkets.<br>
<br>
On the other hand, Nawras, together with Qtel and TDC, the Danish mobile 
operator, has international expertise and experience. Qtel’s highly qualified 
and experienced personnel provided technical, financial and legal expertise to 
the venture. Nawras has adopted elements of Qtel’s new billing system and 
financial services platforms and has a strong IT support system. The advantage 
of international exposure and experience in other markets will go a long way in 
helping Nawras gain a foothold in the Omani market.<br>
<br>
Moreover, TDC has successfully built its networks across 11 other markets, 
including mountainous environments like Oman in countries such as Switzerland 
and Austria. “We are sure that our combined expertise, knowledge and 
technological know-how will help us to build our customer base in Oman to match 
or even surpass GSM subscriber levels in the rest of the GCC,” believes Qtel CEO 
Dr Nasser Marafih, who was in Oman during the launch of Nawras to demonstrate 
the parent’s support and backing for its new offspring.<br>
<br>
Coverage-wise, Nawras claims to extend to over 60 per cent of the population of 
Oman and the company expects to increase it to 95 per cent ‘‘within a short 
period of time’’. Nawras promises to provide Oman with a state-of-the-art EDGE 
network offering superior voice quality and fast data access. The company has 
set a target of $1.7 billion (RO654 million) as total investment during the 
first 10 years, of which $195 million (RO75 million) will be committed in the 
first two years.<br>
<br>
<b>Offers galore</b><br>
In his opening remarks during the launch conference for Nawras, CEO Ross Cormack 
noted that their market research revealed that mobile phone users in Oman wanted 
new and exciting offers, as well as value for their money. ‘‘In a closed market, 
people never have much choice,’’ he said. ‘‘However, with the entry of Nawras, 
many ne<img border="0" src="nawras.jpg" align="right" width="111" height="149">w and exciting offers will be available in the market,’’ he added. <br>
<br>
As expected, Nawras has arrived with price-competitive offers to lure the 
customers. The new kid on the block has created different packages for heavy, 
medium and light users. Its offers are termed Ajel (post-paid), Mousbak and 
MousbakPlus (both pre-paid). The company is offering a three-month honeymoon 
period for its post-paid subscribers, who can call other Nawras subscribers for 
as low as 10 baisas per minute on Fridays. Besides a few sweeteners like the 
three-month offer mentioned above and a standard pricing for MMS (45 baisas 
regardless of the size of the MMS) in the challengers pricing structure, there 
isn’t much to choose between the two operators’ offerings. <br>
<br>
However, the most immediate and important impact of Nawras’ launch has been that 
distance-based pricing is now a thing of the past in Oman. Earlier, Oman Mobile 
used to differentiate its post-paid tariff on the basis of how far the 
subscriber being called was at the time the call was made. However, with Nawras 
launching distance-immune pricing, Oman Mobile was quick to bid adieu to 
distance and introduced standard pricing as well. The company has lined up great 
offers for its post-paid subscribers. The new pricing plan is cheaper and 
simpler with reduced intra-country long-distance calls. With the new price 
chart, the users can call anywhere in the country from 35 baisas at off-peak 
time and the peak price being 45 baisas/minute. To make the offer more 
lucrative, the company has waived off the RO5 installation fee for its post-paid 
users. <br>
<br>
If Nawras is out to lure customers, Oman Mobile is not behind. In fact, just 
days before Nawras launched its services, Oman Mobile extended the validity of 
its popular Hayyak card to 365 days instead of 150 days for all denominations. 
Moreover, a few months after Nawras bagged the licence to operate as the second 
mobile player in the country, beating 28 other contenders, Oman Mobile launched 
Taif services for Hayyak subscribers providing the multimedia services (MMS) 
facilitating picture messages and the general package radio services (GPRS), 
which facilitates the WAP Internet surfing.<br>
<br>
If Nawras is offering roaming facilities for both prepaid as well as postpaid 
services, Oman Mobile offers miss call alert via SMS when the subscriber is off 
the coverage area or off-service handset, call forwarding, call identification, 
multiparty, auto fax and data. If Nawras is offering a 10-baisa-a-minute call 
rate to its subscribers on Fridays, Oman Mobile gave away a special package (Hayyak 
starter kit + a Motorola camera colour screen handset for RO55). As Dr Amer 
Awadh Al Rawas asks, “If these aren’t good offers, what are?” <br>
<br>
<b>Price equation</b><br>
Both the service providers are fighting the offerings’ battle tooth-and-nail, 
and are matching each other’s prices offer by offer. When Nawras announced a 
marginally lower monthly subscription rate of RO3.9 per month for its post-paid 
offering Ajel (as opposed to Oman Mobile’s RO4), the incumbent gave the 
challenger a taste of its own medicine by dropping it to RO3 per month. Of 
course, Nawras quickly made amends and matched the offer to the last baisa 
almost immediately.<br>
<br>
It has been barely a few days in the market, and Nawras has further slashed its 
prices. According to the its revised tariff plan, Mousbak call rate will start 
from 39 baisas per minute (down from the earlier 45 baisas). The activation fee 
for Nawras Ajel has been reduced to RO 14 (from RO19). Besides, sending SMS is a 
lot cheaper now – a flat rate of 10 baisa per SMS from both operators and 45 
baisa per picture message independent of size (by Nawras). <br>
<br>
The call tariff for Hayyak subscribers have been reduced by lowering off-peak 
rate to 45 baisas from the earlier 55 baisas. However, the rate remains the same 
during peak period. Hayyak SMS can be made for 10bzs per day, during both peak 
and off peak hours. Oman Mobile has also reduced the rate of the starter kit 
from RO20 to RO14. It has also announced the introduction of a new budget Hayyak 
RO1.5 top-up voucher. As of now, this is lowest recharge card available in the 
market. Rates for picture message (small MMS) has been reduced from 50 baisas to 
45 baisas. <br>
<br>
<b>Service standards</b><br>
Easily availability and hassle-free payment facilities are the some of the 
important factors that a customer keeps in mind before subscribing to a mobile 
service. To be in business, it is of vital importance that the customer is 
always entertained. It is for this reason that customer service has become very 
important in today’s competitive world. If a company easily rectifies its 
customers’ problems, there are less chances of losing him. <br>
<img border="0" src="omanmobile.jpg" align="right" width="200" height="77"><br>
Keeping in mind these points, both, Nawras and Oman Mobile are in cutthroat 
competition. In such a short span of time, Nawras mobile cards are available at 
450 locations. The company has tied up with premium partners, standard dealers 
and retail partners for distribution. <br>
<br>
Similarly, the payment system has also been simplified. For Nawras, the 
customers can pay at more than 300 locations. For post-paid customers Nawras 
sends an SMS to inform them when the bill has been sent and how much they need 
to pay. The customer can then pay at one of the 9 Nawras stores or at any of 
Bank Muscat’s or Oman Arab Bank’s 130 payment outlets, 155 ATMs and 10 cash 
machines or via Internet and phone banking. The customers can also go online and 
access 24/7 self-care through My Nawras Internet address and the promise is that 
the problems will be attended and solved immediately. For prepaid customers 
Nawras offers lower priced recharge cards at thousands of stores throughout 
Oman.<br>
<br>
Likewise, Oman Mobile is also trying to keep up to customer expectations. The 
company has opened its first full services at Al Araimi complex in Qurum, which 
provides the best services to its customers. Besides, it has also inaugurated 20 
auto-bill payment points. The company has also introduced a mini-SMS bill that 
allows the subscriber to check his/her bill and via SMS by dialling the special 
line 90099, as well as a credit alert for Hayyak subscribers, also by SMS. The 
company has further manned its service counters – it has also increased its call 
centre strength from 30 to 80, where all enquiries will be attended. <br>
<br>
<b>End of an era</b><br>
The mobile communications landscape in Oman has changed for sure. For quite some 
time, Oman Mobile has been the sole player in the mobile telephony market in 
Oman. Not anymore. Times have changed and it’s no longer one man’s pice, From 
now on, it’ll be the price, the coverage and customer service that will 
determine the prospects of both Nawras and Oman Mobile. Both will have to beat 
each other to retain and increase their market share. <br>
<br>
Oman mobile and Nawras . he said, would complement one-another's services and 
infrastructure that will lead to the entry of several new value-added products 
and services that would bene-fit the end users.<br>
<br>
In the meantime, consumer can sit relaxed and ask for more. He has the power to 
keep the two telecom giants running to serve him better. The one with better 
service and rates will win over him. After all it takes two to tango.</font><font face="Marlett" style="font-size: 5pt">g</font></p>

