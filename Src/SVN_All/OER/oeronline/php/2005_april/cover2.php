            Depending on the company’s strategies and plans, and my own 
            intentions and future plans, I can afford to retire without causing 
            the organisation a setback. Our modus of operation is always to 
            avoid complete dependence on any individual in the organisation, in 
            any role. Although this sounds like a very generalised statement, 
            the company’s strategic orientation is in actuality to provide for a 
            sustainable functioning of the company. With my age of slightly 
            short of mid-40s, I do look forward to more than a decade—maybe 
            two—of productive professional life, with the hope to live long 
            enough.<br>
